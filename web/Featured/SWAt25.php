<font face="Times New Roman, Times, serif" size="3"><P>IN APRIL 1977, <I>Socialist Worker</I> published its first issue. Thanks to the hard work of many people, we're going strong today--as a weekly rather than a monthly publication, and with a circulation many times bigger.</P>
<P>Find out more about the history of <I>SW,</I> the rich tradition of publishing left-wing and socialist newspapers--and our plans for spreading the socialist alternative to a bigger and bigger audience:</P>

<P><A HREF="../2002-1/408/408_08_SWAsOrganizer.shtml"><font face="Times New Roman, Times, serif" size="4"><b>A tool for organizing</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3"><I>SW</I> digs beneath the surface of important stories and exposes the lies that the politicians and their media helpers tell us. But <I>Socialist Worker</I> aims to do more. For a revolutionary socialist who is building an organization, a newspaper like <I>SW</I> is also a central organizing tool.</P>

<P><A HREF="../2002-1/402/402_06_SWAgitator.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Agitator and organizer</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">Producing a socialist newspaper is about more than telling the truth. We want to convince readers not only about what's wrong with the world--but that they need to act to change it.</P>

