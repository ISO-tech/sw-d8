<font face="Times New Roman, Times, serif" size="3"><i>Socialist Worker </i>marks the 90th anniversary of the Russian Revolution with this series of articles outlining its course and consequences.<p>

<font face="Times New Roman, Times, serif" size="2"><B>Part 1</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2007-1/617/617_08_StageSet.shtml">How the stage was set for revolution<a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">The Russia of the Tsars was one of history's most terrible dictatorships. Yet it was the setting for a revolution in which millions of people made history.<p>

<font face="Times New Roman, Times, serif" size="2"><B>Part 2</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2007-1/621/621_08_February.shtml">The February Revolution</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">In a matter of one week, the actions of Russian workers and soldiers put an end to three centuries of Tsarist rule.<p>

<font face="Times New Roman, Times, serif" size="2"><B>Part 3</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2007-1/627/627_08_April.shtml">Lenin prepares the Bolsheviks</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">The Russian Revolution of 1917 was largely fought out over the question of which half of "dual power"--the Provisional Government or the soviets--would replace the other.<p>

<font face="Times New Roman, Times, serif" size="2"><B>Part 4</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2007-1/632/632_10_Soviets.shtml">How workers power was organized</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">The workers' councils that arose during the revolution were both a weapon of workers' power against the old system and the beginning of a democratic replacement.<p>

<font face="Times New Roman, Times, serif" size="2"><B>Part 5</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2007-1/635/635_10_National.shtml">The revolt against the Tsar's empire</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">How did the Russian Revolution affect the vast empire of the Tsar, with tens of millions living under an occupation by a government whose language and laws were not their own.<p>

<font face="Times New Roman, Times, serif" size="2"><B>Part 6</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2007-2/637/637_13_JulyDays.shtml">The revolution gains strength</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">As other parties in Russia compromised themselves in the eyes of the masses, the Bolsheviks faced difficult tactical questions related to the uneven character of the struggle.<p>

<font face="Times New Roman, Times, serif" size="2"><B>Part 7</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2007-2/639/639_13_Repression.shtml">Repression and resurgence</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">The period of reaction that followed the July Days was relatively short-lived, and the movement bounced back in a matter of a month--stronger, deeper and broader.<p>

<font face="Times New Roman, Times, serif" size="2"><B>Part 8</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2007-2/643/643_10_Kornilov.shtml">How Kornilov was defeated</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">With the government paralyzed, the defense of Petrograd from the threat of a military coup depended on a mass popular mobilization led by Russian workers.<p>

<font face="Times New Roman, Times, serif" size="2"><B>Part 9</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2007-2/646/646_10_Party.shtml">The party and the revolution</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">Hundreds of studies by Russia "experts" portray Lenin and Bolsheviks as ruthless and authoritarian. But the truth is the party won the allegiance of Russia's most militant workers.<p>

<font face="Times New Roman, Times, serif" size="2"><B>Part 10</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2007-2/650/650_10_October.shtml">The final act of the revolution</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">In the end, the process by which workers and soldiers took power in Petrograd was simple. "It was not necessary to employ force," Trotsky later wrote, "for there was no resistance."<p>

<font face="Times New Roman, Times, serif" size="2"><B>Part 11</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2007-2/653/653_10_Legacy.shtml">The legacy of 1917</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">Even as a memory of a struggle from a distant time under very different conditions, the Russian Revolution remains a threat to defenders of the status quo.<p>

