<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A socialist newspaper to fight for our side</b></font> </P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by PAUL D'AMATO</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 14, 2001 | Page 13</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>THE ROLE of the mainstream press, to quote Karl Marx, is to reflect "the ruling ideas of society." <I>New York Times</I> journalist John Swinton put it more colorfully at his retirement party. "The business of a journalist now is to destroy the truth, to lie outright, to pervert, to vilify, fall at the feet of Mammon and sell himself for his daily bread," Swinton said.</P>
<P>"We are tools, vessels of rich men behind the scenes, we are jumping jacks. They pull the strings--we dance."</P>
<P>The press isn't always blatantly on the side of the rich and powerful--though often it is, as when it automatically sides with the U.S. in every international dispute. Usually, though, it tries to present itself as a provider of "unbiased" and "balanced" news, as a recent report in the <I>Chicago Tribune</I> about a dispute between California nurses and hospitals.</P>
<P>"Nurses are seeking high staff numbers, asserting that patient care is compromised when nurses are overworked," the <I>Tribune</I> wrote. "Hospitals want lower numbers, arguing that the high cost of beefing up nursing staffs may harm patient care by forcing institutions to cut other services."</P>
<P>The nurses' very reasonable argument is "asserted." Meanwhile, management's real motives--it doesn't want to spend more money--are covered up. Examples like these show that people fighting for a better world can't rely on the bourgeois media for the truth.</P>
<P>We need our own press--our own way to disseminate ideas that challenge "the ruling ideas of society." This has long been understood by radical and revolutionary movements.</P>
<P>The radical wing of the French Revolution of the late 18th century produced a newspaper called <I>Pere Duchesne</I> (<I>Father Duchesne</I>), a popular paper in which a fictional artisan offered his opinions about the debates and struggles of the movement.</P>
<P>In the U.S., the socialist newspaper <I>Appeal To Reason, </I>published in Kansas starting in 1896, reached a weekly circulation of 750,000. In Italy, Antonio Gramsci and other revolutionary socialists produced <I>L'Ordine Nuovo</I> (<I>New Order</I>) during the early 1900s to influence the factory committee movement. The connection of these papers to the actual struggles has differed from case to case.</P>
<I><P>Appeal to Reason,</I> for example, wasn't an official Socialist Party publication. The Socialist Party didn't produce its own central newspaper because its composition was too diverse--including both militant syndicalists and moderate reformers, both opponents and supporters of immigration, both racists and antiracists. Moreover, as an organization committed mainly to getting socialists elected to public office, a central directing organ, dedicated to influencing the course of struggles on the ground, was unnecessary.</P>
<P>But a revolutionary organization that aims to work in and influence day-to-day class battles must have a different kind of press. Writing in the early 1900s, Lenin was first to develop a systematic idea of the role of a newspaper in the fight for socialism. He envisioned an organization "capable of uniting all forces and guiding the movement in actual practice...an organization ready at any time to support every protest and every outbreak and use it to build up and consolidate the fighting forces suitable for the decisive struggle."</P>
<P>For that, he argued, Russia needed an "all-Russian" national paper. The purpose of such a paper wasn't just to counter the other side's lies. It also had to agitate--to argue at each and every turn what the next step in the struggle should be. Beyond this, the paper could act as a means of organizing a network of militants.</P>
<P>Lenin likened a national newspaper to a scaffolding, a structure that facilitates the construction of a building and communication between builders. Lenin conceived of a newspaper as a means to "train its members to follow political events carefully, appraise their significance and their effect on various strata of the population, and develop effective means for the revolutionary party to influence those events."</P>

