<font face="Times New Roman, Times, serif" size="4"><b><a href="../2006-2/596/596_10_Montgomery.shtml">The civil rights movement after Montgomery</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">The early days of the African-American civil rights movement, when activists faced widespread resistance from supporters of Jim Crow, have important lessons for today.<p>

