<a name="Class"></a><p><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>CLASS STRUGGLE U.S.A.</b></font></P>

<font face="Times New Roman, Times, serif" size="4"><b><a href="../2007-2/640/640_08_BonusArmy.shtml">The march of the Bonus Army</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">With the Great Depression causing mass unemployment, some 40,000 war veterans made their way to Washington, D.C., in 1932 to demand an immediate bonus payment.<p>

<font face="Times New Roman, Times, serif" size="4"><b><a href="../2007-2/639/639_07_UPS.shtml">When Big Brown was shut down</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">The Teamsters strike at UPS 10 years ago delivered labor movement's first decisive victory in decades and pointed a different way forward for unions.<p>

<font face="Times New Roman, Times, serif" size="4"><b><a href="../2007-2/638/638_10_1877.shtml">America's first nationwide strike</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">The Great Railroad Strike of 1877 spread like a wildfire from Baltimore to Pittsburgh to St. Louis and Chicago--and was only defeated with utmost brutality on the part of the government.<p>

<font face="Times New Roman, Times, serif" size="4"><b><a href="../2007-1/629/629_08_MayDay.shtml">The first May Day</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">The 19th-century struggle of a largely immigrant working class in the U.S. for the eight-hour day is honored by the international workers' holiday on May 1.<p>

<font face="Times New Roman, Times, serif" size="3"><b>The defeat of PATCO:</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2006-2/600/600_08_PATCO.shtml">Signal of the employers' offensive</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">In just five small letters, one event encapsulates the employers' offensive of the 1980s--PATCO.<p>

<font face="Times New Roman, Times, serif" size="3"><b>Striking the textile towns of Lawrence and Paterson</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2006-2/595/595_10_WorkersUnited.shtml">The power of workers united</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">Two strikes in the textile industry--in Lawrence, Mass., in 1912 and Paterson, N.J., in 1913--illustrated the potential for workers of many different nationalities to build solidarity.<p>

<font face="Times New Roman, Times, serif" size="3"><b>The hidden history of workers' struggle in the U.S.</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2006-1/589/589_08_Subterranean.shtml">Subterranean Fire</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3"><i>SW </i>columnist Sharon Smith is the author of a new book that tells the hidden history of working-class radicalism in the U.S. Here, we reprint excerpts from <i>Subterranean Fire</i>.<p>

<font face="Times New Roman, Times, serif" size="3"><b>A hundred years of the Industrial Workers of the World</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2005-2/550/550_10_IWW.shtml">One big union</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">Many of the questions faced by the union movement today were debated 100 years ago at the founding convention of the Industrial Workers of the World.<p>

<font face="Times New Roman, Times, serif" size="3"><b>Howard Zinn and Anthony Arnove on:</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2004-2/519/519_07_Voices.shtml">Voices of a People's History</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">The authors of a wonderful new book that helps reveal the other side of history talk to <i>Socialist Worker.</i> <p>

<P><font face="Times New Roman, Times, serif" size="3"><b>Kurt Vonnegut on the socialists who fought for workers</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../Featured/Stories/382_08_VonnegutFull.shtml">Celebrating socialism in the U.S.</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">Kurt Vonnegut is one of America's best-known novelists. In October, Vonnegut was given the Carl Sandburg Literary Award from the Friends of the Chicago Public Library--and used his acceptance speech to discuss Sandburg's commitment to socialism.</P>

<P><font face="Times New Roman, Times, serif" size="3"><b>The story of the great Lawrence textile strike of 1912</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../Featured/Stories/390_08_LawrenceStrike.shtml">Bread and roses</A></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">James Oppenheimer wrote the song "Bread and Roses" to honor the great strike of textile workers in Lawrence, Mass., 90 years ago this month. He took the title from the banners of strikers, who demanded not just decent treatment at work, but the right to dignity and a better quality of life.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Framed and put to death because of their political beliefs</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="../2002-2/418/418_08_SaccoAndVanzetti.shtml">Sacco and Vanzetti</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">Seventy-five years ago this month, Nicola Sacco and Bartolomeo Vanzetti--two Italian immigrants--were executed in the state of Massachusetts. But not before the case and the movement that was organized to save them made a huge impact around the world.</P>

<P><font face="Times New Roman, Times, serif" size="3"><b>When San Francisco longshore workers beat the bosses</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="../2002-2/419/419_08_Dockworkers.shtml">War on the docks</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">The International Longshore and Warehouse Union is facing a tough battle against the port bosses on the West Coast docks. Union leaders need to remember the tactics that won the 1934 strike to win union recognition in the ports.</P>

