<font face="Times New Roman, Times, serif" size="4"><b><a href="../2006-1/578/578_04_GeorgeJackson.shtml">The assassination of George Jackson</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">Stephen Bingham, a lawyer who worked with George Jackson, talks to <i>SW </i>about the murdered prison activist's legacy--and his own fight for justice.<p>

<font face="Times New Roman, Times, serif" size="3"><b>United States Prisoner No. 89637-132</b></font><br>
