<font face="Times New Roman, Times, serif" size="3">The way films like <i>10,000 B.C. </i>depict early societies sheds a lot of light on how human history is mangled.<p>

<font face="Times New Roman, Times, serif" size="4"><b><a href="../2008-1/658/658_08_CIA.shtml">Specialists in coups and killing</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">In light of the tendency of some liberals to identify Bush's critics within the CIA as the "good guys," it's worth recalling the agency's real history.<p>

