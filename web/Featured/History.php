<font face="Times New Roman, Times, serif" size="3">Che Guevara's image has inspired millions of people who want to fight for a better world. But very few people, especially in the U.S., know much about his life or politics.<p>

<font face="Times New Roman, Times, serif" size="4"><b><a href="../2007-1/631/631_10_JamesConnolly.shtml">Ireland's greatest revolutionary</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">Ireland's political establishment canonizes James Connolly as a great "patriot." But they would feel ill at ease with his lifelong commitment to working class self-emancipation.<p>

<font face="Times New Roman, Times, serif" size="3"><b>The uprising that shook Eastern Europe's tyrants</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2006-2/608/608_10_Hungary.shtml">Hungary '56</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">The Hungarian Revolution of 1956 represented an uprising from below against an oppressive system that ruled in the name of socialism.<p>

<font face="Times New Roman, Times, serif" size="4"><b><a href="../2006-2/607/607_10_Germany.shtml">Revolution in Germany</a></b></font><br>
