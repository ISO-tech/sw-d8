<font face="Times New Roman, Times, serif" size="3"><b>The rebellion that revived South Africa's liberation struggle</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2006-2/595/595_08_Soweto.shtml">Soweto 1976</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">Thirty years ago, Black students in South Africa opened a new period of resistance to the racist apartheid system when they rose up to protest oppression in the schools.<p>

<font face="Times New Roman, Times, serif" size="3"><b>Fifteen months in 1980-81:</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2006-1/570/570_08_Poland.shtml">The story of Solidarnosc</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">The Solidarnosc workers rebellion in Poland in 1980-81 stands with the great revolutionary upheavals of the past in showing the promise of a new society based on workers' power.<p><p>

<font face="Times New Roman, Times, serif" size="3"><b>"The great dress rehearsal"</b></font><br>
