<font face="Times New Roman, Times, serif" size="4"><b><a href="../2005-1/531/531_06_MalcolmX.shtml">Malcolm X: Legacy of a revolutionary</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">Malcolm X, who was assassinated 40 years ago, was a revolutionary and internationalist who confronted questions of imperialism and racism that remain with us today.<p>

