<font face="Times New Roman, Times, serif" size="3">One hundred years ago, the Russian working class took center stage for the first time with strikes and demonstrations that shook the power of the dictatorial tsar.<p>

<font face="Times New Roman, Times, serif" size="4"><b><a href="../2004-2/521/521_10_Bolsheviks.shtml">How the Russian Revolution was won</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">The real history of the Russian Revolution is about ordinary people coming to life as they discovered their own potential for creating a truly democratic society.<p>

<P><font face="Times New Roman, Times, serif" size="3"><b>The alternative to a system controlled by a tiny few</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><A HREF="../2002-2/421/421_08_WorkersCouncils.shtml">How will workers run society?</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">"Nice idea, but it will never happen." For many people, this is the common-sense response to socialism--a society based not on greed and violence, but on cooperation and fulfilling human need. But history provides many examples of workers uniting and fighting back.</P>

<P><font face="Times New Roman, Times, serif" size="3"><b>How Russia went from a workers' state to state capitalism</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2003-2/462/462_08_Stalin.shtml">Why did Stalin rise to power?</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">Joseph Stalin is rightly remembered as one of the most horrible tyrants of the 20th century. But to most people, his name is also associated with socialism.</P>

<P><font face="Times New Roman, Times, serif" size="3"><b>Thirty years after the coup that toppled a left-wing government</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2003-2/467/467_08_Chile.shtml">Chile's tragedy</a></b></font><BR>
