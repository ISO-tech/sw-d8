<font face="Times New Roman, Times, serif" size="3"><b>Forty years after King's last stand</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="../2008-1/668/668_06_King.shtml">The unfinished struggle</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">Martin Luther King was assassinated in the midst of a struggle that he saw as part of the next stage for the civil rights movement.<p>

