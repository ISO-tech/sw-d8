<P><font face="Times New Roman, Times, serif" size="5"><b><I>Socialist Worker</I> goes weekly</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>BEGINNING WITH our October 12 issue, <I>Socialist Worker</I> began appearing weekly--bringing you all the news and analysis that you've come to expect, only twice as often.</P>
<P>We're excited about this step that <I>SW </I>has taken. But we need the help of all of our readers. We need reports from all of the struggles that take place--in workplaces, in communities, on campuses. We need contributions that take up the questions facing our struggles. And we need your financial help. Send your check to: <I>SW,</I> P.O. Box 16085, Chicago, IL 60616.</P>
<P>Papers like <I>Socialist Worker</I> rely on their readers--what they experience, what they think and what they plan to do about it. And we're proud to present all this twice as often.</P>
<P>The need for a socialist newspaper--to be a voice for our side, to report on the news of our struggles and to discuss the challenges ahead--has never been greater.</P>

