<P><font face="Times New Roman, Times, serif" size="4">S&iacute; de la ONU no justifica guerra contra Irak</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>&#161;Dile a Bush que NO!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">20 de septiembre de 2002 | P&aacute;gina 1</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>SEGUN SE acercaba el aniversario de los ataques del 11 de septiembre, la administraci&oacute;n de Bush planeaba conmemoraciones sombr&iacute;as para las v&iacute;ctimas.</P>
<P>Pero no habr&aacute; conmemoraciones para los miles de civiles inocentes afganos matados por las bombas de los EE.UU.  Tampoco nadie mencionar&aacute; a los centenares de j&oacute;venes metidos en c&aacute;rceles de los EE.UU. o deportados por ninguna otra raz&oacute;n que la de ser &aacute;rabes o musulmanes.</P>
<P>Lo que s&iacute; hubo y habr&aacute; es un esfuerzo calculado y c&iacute;nico por la pandilla de Bush de acumular m&aacute;s presi&oacute;n a favor de la pr&oacute;xima fase de su "guerra contra el terror"--una guerra contra Irak que causar&aacute; destrucci&oacute;n y derramamiento de sangre indescriptibles.</P>
<P>No es coincidencia que el Departamento de Justicia escogi&oacute; poco antes del 11 de septiembre para anunciar acusaciones contra seis hombres por "brindar apoyo material o recursos" a terroristas.</P>
<P>&#191;Qui&eacute;nes son estos hombres? Uno es Farouk Ali Haimoud--que trabaj&oacute; en una tienda de helados en el aeropuerto de Detroit. El fue detenido el oto&ntilde;o pasado, pero los fiscales no pudieron encontrar ninguna evidencia en contra suya.</P>
<P>Pero mientras los oficiales federales cacareaban acerca de las acusaciones, apenas se mencionaba al doctor de Florida que fue agarrado recientemente tramando el volar docenas de mezquitas. Para los EE.UU., las vidas &aacute;rabes no valen lo mismo.</P>
<P>Esto est&aacute; claro en base al m&aacute;s reciente empuje por una nueva guerra contra Irak. La Guerra del Golfo de 1991 dej&oacute; cerca de 200,000 iraqu&iacute;es muertos en cuesti&oacute;n de mes y medio--y una d&eacute;cada de sanciones ha matado a 1 mill&oacute;n m&aacute;s. </P>
<P>Pero la administraci&oacute;n de Bush no va a estar feliz hasta que "pueda volver y completar el trabajo". El vicepresidente Dick Cheney hizo esto obvio cada vez que se acerc&oacute; a un podio la &uacute;ltima semana de agosto--para vociferar acerca de la necesidad de un "cambio de r&eacute;gimen" en Irak, no importa el costo.</P>
<P>Las figuras m&aacute;s prominentes del establecimiento en Washington pueden diferir acerca del cu&aacute;ndo y del c&oacute;mo--pero la mayor&iacute;a de los republicanos y los dem&oacute;cratas est&aacute;n de acuerdo con la guerra. Eso los separa del n&uacute;mero creciente de personas que cuestiona la campa&ntilde;a pro-guerra de Bush.</P>
<P>Cuando Bush habl&oacute; frente a la ONU el 12 de septiembre, su chantaje fue sencillo: "O aprueban y le dan legitimidad a la guerra contra Irak, o lo hacemos sin ustedes". Despu&eacute;s de todo la ONU es la instituci&oacute;n a cargo de la guerra camuflajeada de sanciones econ&oacute;micas que han matado lentamente a tantos civiles iraqu&iacute;es durante los &uacute;ltimos 10 a&ntilde;os. La ONU no es una fuerza a favor de la paz, sino una fachada para la guerra.</P>
<P>El chantaje de Bush ahora se ha enfocado en las supuestas armas nucleares de Saddam Hussein. Y como siempre, si no tienen ninguna evidencia, pues se la inventan. Como cuando los portavoces de Bush alegaron que la Agencia Internacional de Energ&iacute;a At&oacute;mica inform&oacute; en 1998 que Irak estaba a s&oacute;lo seis meses de poder desarrollar armas nucleares.</P>
<P>Pero la verdad es que ese informe concluy&oacute; lo opuesto--que hab&iacute;a cero posibilidad de que esto ocurriera. Un sondeo conjunto de la revista <I>Time </I>y de CNN a principios de septiembre encontr&oacute; que el apoyo a una invasi&oacute;n de Irak hab&iacute;a ca&iacute;do a apenas un 51 por ciento de los entrevistados, habiendo bajado de 70 por ciento durante el pasado oto&ntilde;o. Los activistas planearon m&aacute;s de 100 vigilias y protestas anti-guerra en unos 36 estados para la semana del 11 de septiembre.</P>
<P>La consigna del movimiento anti-guerra hace un a&ntilde;o atr&aacute;s era "Que no conviertan la tragedia en guerra". Esto es aun tan pertinente en las pr&oacute;ximas semanas, cuando Bush y compa&ntilde;&iacute;a tratar&aacute;n de explotar el 11 de septiembre otra vez para presionar por una invasi&oacute;n de Irak. Es hora de decirle a Bush: &#161;No a la guerra y no al racismo!</P>

