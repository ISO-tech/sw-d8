<P><font face="Times New Roman, Times, serif" size="5"><b>Victoria contra hoteles de Chicago</b></font></P>

<P><font face="Times New Roman, Times, serif" size="2"><b>Por Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | 20 de septiembre de 2002 | P&aacute;gina 4</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--Despu&eacute;s de amenazar con declarar la primera huelga contra los hoteles en la historia de Chicago a principios de septiembre, m&aacute;s de 7,000 trabajadores de hotel ganaron un aumento significativo de sueldo y una reducci&oacute;n en la prima de su plan m&eacute;dico.</P>
<P>Los miembros del Sindicato de Empleados de Hoteles y Restaurantes (HERE por sus siglas en ingl&eacute;s), Locales 1 y 450, que trabajan en 27 hoteles del &aacute;rea, aprobaron su nuevo contrato el 5 de septiembre. Si se hubieran ido a la huelga pudieron haber ganado m&aacute;s--pero a&uacute;n as&iacute; lo que ganaron no es peque&ntilde;a cosa.</P>
<P>Los trabajadores, que se ganaban apenas $8.83 por hora, recibir&aacute;n un aumento de $3.27 durante los cuatro a&ntilde;os del contrato. Seg&uacute;n los oficiales de la uni&oacute;n, esto equivale un incremento promedio de 54.4 por ciento entre sueldo y beneficios durante los pr&oacute;ximos cuatro a&ntilde;os.</P>
<P>Adem&aacute;s, ganaron cuatro d&iacute;as por enfermedad, y un receso con paga de 15 minutos diarios-- beneficios que no ten&iacute;an antes de este contrato. El acuerdo tambi&eacute;n requiere que los hoteles negocien con la uni&oacute;n en caso de cualquier subcontrataci&oacute;n de trabajo a empresas externas. Desde el principio el sindicato hab&iacute;a puesto sus miras altas. Y estaban listos para irse a la huelga. </P>
<P>El 12 de agosto, 4,000 miembros votaron con un margen de 98 por ciento apoyando una huelga. La siguiente semana, tambi&eacute;n unos 4,000 marcharon a lo largo de la lujosa Avenida Michigan. Esto ha sido un gran adelanto para el Local 1, cuyo liderato fue plagado por muchos a&ntilde;os por sus v&iacute;nculos con el crimen organizado, malversaci&oacute;n de las cuotas de los miembros, y p&eacute;simos contratos para los trabajadores. </P>
<P>Henry Tamarin fue elegido presidente del local  el a&ntilde;o pasado, y ha implementado cambios importantes en la uni&oacute;n. Emple&oacute; a organizadores biling&uuml;es para comunicarse con el gran n&uacute;mero de miembros hispanohablantes. </P>
<P>Pero cuando lleg&oacute; la fecha l&iacute;mite de declarar la huelga, &eacute;l accedi&oacute; a seguir negociando cuando intervinieron el presidente de la Federaci&oacute;n del Trabajo de Chicago Dennis Gannon y el gobernador de Illinois George Ryan. Esto fue un error. Al continuar las negociaciones sin irse a la huelga luego de la expiraci&oacute;n del viejo contrato, HERE dej&oacute; pasar una oportunidad para ense&ntilde;arles a los gerentes abusivos una lecci&oacute;n. </P>
<P>Aun as&iacute;, este contrato constituye una gran victoria para unos de los trabajadores peor pagados de Chicago.</P>

