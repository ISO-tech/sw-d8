<P><font face="Arial, Helvetica, sans-serif" size="2"><b>LO QUE PENSAMOS</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Los dem&oacute;cratas no merecen tu voto</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">20 de septiembre de 2002 | P&aacute;gina 2</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>Apenas dos meses antes de las elecciones en los EE.UU. el ambiente pol&iacute;tico ha cambiado. Por m&aacute;s de un a&ntilde;o el presidente Bush se ha beneficiado del impacto de los ataques del 11 de septiembre, seg&uacute;n reflejan las encuestas.</P>
<P>Ahora, con su popularidad deterior&aacute;ndose, &eacute;l no parece tan invencible. El impacto que el 11 de septiembre produjo nubl&oacute;, pero no elimin&oacute;, las preocupaciones b&aacute;sicas de la gente com&uacute;n. Ya en enero, las encuestas mostraban a la econom&iacute;a nacional a la par con el terrorismo en lo que se refiere a las principales preocupaciones de los estadounidenses.</P>
<P>Los espectaculares colapsos de Enron, WorldCom, Global Crossing, Arthur Andersen y muchas otras corporaciones de prestigio desenmascaron la corrupci&oacute;n que es parte esencial del sistema econ&oacute;mico. Entonces las trillonarias p&eacute;rdidas de la bolsa de valores--tan s&oacute;lo en julio--causando la desaparici&oacute;n de todos los fondos de retiros en estas empresas, hizo que la ansiedad econ&oacute;mica se convirtiera en un t&oacute;pico candente.</P>
<P>El desempleo se mantiene a un alto nivel mientras que la econom&iacute;a virtualmente ya no produce m&aacute;s empleos. Un informe del gobierno en julio mostr&oacute; que los jefes planean despedir a casi 250,000 trabajadores m&aacute;s en la segunda mitad del 2002. De igual forma, los gobiernos estatales y municipales est&aacute;n implementando recortes masivos en el presupusto de educaci&oacute;n y en los programas sociales esenciales.</P>
<P>En vista a lo acontecido este a&ntilde;o, es una sorpresa que el partido republicano no est&eacute; tambale&aacute;ndose hacia una derrota contundente. Afortunadamente para los republicanos, su principal partido "opositor" es el partido dem&oacute;crata. Mientras los dem&oacute;cratas se representan como el partido del pueblo, se llenan sus bolsillos con dinero de las corporaciones de la misma forma que los republicanos.</P>
<P>Los l&iacute;deres dem&oacute;cratas del senado ayudaron a aprobar la ley represiva llamada "USA Patriot Act"el pasado octubre, y los dem&oacute;cratas de ambas c&aacute;maras han votado con benepl&aacute;cito a favor del enorme incremento al presupuesto del Pent&aacute;gono que Bush propuso. A pesar de su ret&oacute;rica, llevan a&ntilde;os sin producir una ley que subsidie las recetas m&eacute;dicas de los envejecientes atrav&eacute;s del programa Medicare.</P>
<P>Los dem&oacute;cratas y los liberales est&aacute;n atrapados en una camisa de fuerza que ellos mismos confeccionaron. Prometen soluciones a algunas preocupaciones de la vida diaria, como por ejemplo el desempleo y el Medicare.</P>
<P>Pero rehusan oponerse a las pol&iacute;ticas de Bush que interfieren con esto--como los recortes a los impuestos de los ricos y el militarismo. A la vez que las elecciones se acercan, los dem&oacute;cratas buscar&aacute;n apoyo entre los trabajadores, los pobres y las minor&iacute;as raciales. Y este a&ntilde;o est&aacute;n haciendo un esfuerzo especial en perseguir el voto latino para sus candidatos a puestos importantes en Texas, California y Nevada.</P>
<P>Los dem&oacute;cratas no merecen nuestro apoyo. Despu&eacute;s de que el gobernador de California, Gray Davis, recab&oacute; el apoyo de los trabajadores agr&iacute;colas en las elecciones del 1998, en agosto ense&ntilde;&oacute; de que lado &eacute;l en verdad est&aacute;. Davis apoy&oacute; a los patrones agr&iacute;colas en su intento de reducir los derechos de los trabajadores del campo.</P>
<P>Los dem&oacute;cratas hablan muy bonito, pero en &uacute;ltima instancia siempre terminan sirvi&eacute;ndoles a sus amos de las corporaciones. Para conseguir los cambios que realmente mejoren nuestras vidas, la gente com&uacute;n tiene que tomar la iniciativa de envolverse en las luchas diarias en sus lugares de trabajo y en sus comunidades.</P>

