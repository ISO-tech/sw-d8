<P><font face="Times New Roman, Times, serif" size="5"><b>Argentina despu&eacute;s de las elecciones </b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Por Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | 13 de junio de 2003</font></p>

<P><font face="Times New Roman, Times, serif" size="3">CUANDO LA Casa Blanca comience a cotorrear acerca de transformar a Irak en otra exitosa historia del mercado libre, recuerden a Argentina. </P>
<P>Antes elogiada como la mejor alumna del Fondo Monetario Internacional (FMI), Argentina es ahora desastre econ&oacute;mico. Sesenta por ciento de su poblaci&oacute;n vive bajo la l&iacute;nea de pobreza y la malnutrici&oacute;n es rampante en un pa&iacute;s que es uno de los m&aacute;s grandes exportadores de comida.</P>
<P>Y la pr&oacute;xima vez que los propagandistas de Washington declaren que las reformas de mercado equivalen a democracia, consideren el desastre de la &uacute;ltima elecci&oacute;n en Argentina. Despu&eacute;s de obtener la mayor&iacute;a de los votos en la primera vuelta, pero no los suficientes para ganarla, el ex-presidente argentino Carlos Menem se retir&oacute; de las elecciones para evitar la derrota a manos de N&eacute;stor Kirchner.</P>
<P>Estas elecciones fueron las primeras desde Diciembre 2001, cuando un masivo levantamiento forz&oacute; la renuncia del presidente Fernando de la R&uacute;a y de otros tres presidentes interinos en los d&iacute;as siguientes. El Congreso argentino impuso a Eduardo Duhalde, un viejo rival de Menem, como presidente. Las continuas protestas ataron la manos de Duhalde, forz&aacute;ndolo a organizar nuevas elecciones, a las cuales no pudo presentarse como candidato. Duhalde ofreci&oacute; su apoyo a Kirchner, un gobernador provincial qui&eacute;n hizo una campa&ntilde;a populista basada en promesas de ayudar a los pobres.</P>
<P>Duhalde, Menem, y Kirchner son todos peronistas, miembros del partido fundado en los a&ntilde;os cuarenta por Juan Per&oacute;n. Menem, qui&eacute;n se gan&oacute; el respeto de Clinton y de las corporaciones estadounidenses cuando privatiz&oacute; la industria estatal cuando era presidente, trat&oacute; de volver a la arena pol&iacute;tica haciendo un llamado por la "ley y el orden" y por la dura represi&oacute;n en contra de las protestas callejeras.</P>
<P>Pero su corrupci&oacute;n (Menem fue brevemente puesto bajo arresto domiciliario despu&eacute;s que dej&oacute; la presidencia) limit&oacute; su alcance a un 24 por ciento del electorado en la primera vuelta. Retir&aacute;ndose de la carrera electoral, Menem espera corroer la legitimidad de Kirchner, incluso algunos comentaristas pol&iacute;ticos lo acusan de estar planeando un golpe de estado.</P>
<P>De hecho, la campa&ntilde;a de Menem ya ayud&oacute; a deslizar el debate pol&iacute;tico en Argentina hacia la derecha. En las &uacute;ltimas semanas antes de las elecciones, un juez aprob&oacute; el desalojo de la f&aacute;brica textil Brukman por la polic&iacute;a. &Eacute;sta era una de las 100 plantas a trav&eacute;s del pa&iacute;s que hab&iacute;an sido ocupadas y mantenidas por sus trabajadores luego que sus patrones las cerraron en medio del colapso econ&oacute;mico.</P>
<P>Cuando Duhalde no intent&oacute; interferir con el desahucio, esto fue una se&ntilde;al para el empresariado argentino, y tambi&eacute;n para los bancos y corporaciones occidentales, que la acci&oacute;n pol&iacute;tica de las masas ser&iacute;a detenida, no importa qui&eacute;n asumiera la presidencia. Desafortunadamente, la izquierda revolucionaria, la cual ha jugado un significativo papel en las protestas masivas, fracas&oacute; en presentar un frente unido en las elecciones, con algunos grupos participando y otros llamando al boicot.</P>
<P>Kirchner ser&aacute; pronto atrapado entre sus promesas de reforma y la presi&oacute;n para pagar la deuda externa argentina de $170 mil millones. Esto s&oacute;lo instigar&aacute; nuevas luchas, las que pueden presentar un desaf&iacute;o a la las premisas del libre mercado y al corrupto sistema pol&iacute;tico que las ampara.</P>

