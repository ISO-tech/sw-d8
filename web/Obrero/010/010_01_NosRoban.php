<P><font face="Times New Roman, Times, serif" size="4">Exprimen a los pobres para dar a los ricos</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Nos roban el futuro</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">13 de junio de 2003 | P&aacute;gina 1</font></p>

<P><font face="Times New Roman, Times, serif" size="3">CREAR EMPLEOS. Hacer el sistema de impuestos m&aacute;s justo. Ayudar a las familias de EE.UU. que sufren por la debilidad de la econom&iacute;a.</P>
<P>Todo era una pila de mentiras. El verdadero prop&oacute;sito del m&aacute;s reciente regalo de recortes de impuestos de George W. Bush se hizo cristalino cuando los l&iacute;deres republicanos del Congreso--y los mercenarios de la Casa Blanca que colaboraron con ellos--a&ntilde;adieron un cambio de &uacute;ltima hora al proyecto de ley para negarles los beneficios del recorte a 6.5 millones de familias que sobreviven gan&aacute;ndose el salario m&iacute;nimo o un poco m&aacute;s.</P>
<P>Estos se sumaron a otros millones de contribuyentes que se quedaron colgando cuando los regateros de Washington se reunieron tras bastidores para negociar un acuerdo final. En total, 50 millones de hogares--m&aacute;s de uno de cada tres a lo largo de EE.UU., casi todos de bajos ingresos--no van a recibir ni un centavo de la nueva ley contributiva de Bush.</P>
<P>Pero para los ya super ricos, Washington tiene otro obsequio--una eterna fuente de alegr&iacute;as. El meollo de la legislaci&oacute;n es una reducci&oacute;n en la tasa de impuestos sobre los dividendos de las acciones y de las ganancias sobre bienes capitales, pagados casi exclusivamente por gente con los ingresos m&aacute;s altos. Gente como...digamos, Dick Cheney, quien se presta a atragantarse unos $100,000 por a&ntilde;o o m&aacute;s.</P>
<P>"Quiz&aacute;s ahora", escribi&oacute; el columnista del <I>Boston Globe, </I>Thomas Oliphant, "algunas personas a lo mejor comiencen a darse cuenta de que la verdadera lucha de clases en los Estados Unidos se ha emprendido siempre contra los que trabajan a tiempo completo por sueldos modestos, y que las golosinas de los ricos son literalmente sacadas de sus bolsillos".</P>
<P>Avergonzados por los informes de prensa que claramente identificaban quienes ser&aacute;n los perdedores con la nueva ley contributiva, los republicanos culparon a los dem&oacute;cratas por exigir que el costo final de la legislaci&oacute;n no sobrepasara los $350 mil millones. As&iacute; que los legisladores sacrificaron a las familias de bajos ingresos para "ahorrarse" $3.5 mil millones, o el 1 por ciento del costo final de la medida legislativa.</P>
<P>La determinaci&oacute;n de la pandilla de Bush para robarle al pueblo trabajador para darle a los ricos no puede ser m&aacute;s obvia. Pero estos embaucadores tambi&eacute;n tienen otra meta en mente. </P>
<P>Al forzar dos de los tres m&aacute;s grandes recortes en impuestos en la historia de EE.UU. desde que tom&oacute; el poder, el gobierno de Bush est&aacute; sometiendo al gobierno federal a una estrangulaci&oacute;n financiera por las pr&oacute;ximas d&eacute;cadas. A menos que los recortes de los impuestos se revoquen, es s&oacute;lo cuesti&oacute;n de tiempo antes de que Washington comience a imponer recortes dr&aacute;sticos en programas de los que la mayor&iacute;a de los trabajadores depende--el Seguro Social, el Medicaid y el Medicare.</P>
<P>Ya el impacto se ha sentido. Por ejemplo, la administraci&oacute;n ha reducido los fondos para educaci&oacute;n que Bush prometi&oacute; en su fanfarroneada ley "Ning&uacute;n Ni&ntilde;o Dejado Atr&aacute;s". En Oklahoma, los oficiales creen que m&aacute;s del 40 por ciento de los programas requeridos por esta ley no tienen fondos suficientes. </P>
<P>"Vamos a estar tan pobremente equipados para satisfacer esos est&aacute;ndares que da miedo", dijo Stacy Martin, de la Asociaci&oacute;n de Educadores de Oklahoma. "Entre los maestros de Oklahoma, el esquema de Bush es referido en broma  como 'Ninguna Escuela Dejada Sobreviviendo'".</P>
<P>La oposici&oacute;n de los dem&oacute;cratas a los recortes de impuestos de Bush ha sido totalmente inadecuada. Por lo general, los l&iacute;deres del "partido del pueblo" aceptan que algunos recortes de impuestos son necesarios--cuando lo que se necesita realmente es <I>aumentos</I> de impuestos para los super ricos que han gozado de estas estafas. </P>
<P>No podemos dejar que Bush se salga con la suya y nos robe nuestro futuro.</P>

