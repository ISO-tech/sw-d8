<P><font face="Times New Roman, Times, serif" size="5"><b>"Tenemos que defender nuestros derechos"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">5 de abril de 2002 | P&aacute;gina 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>Quiz&aacute;s unas pocas libertades civiles desfiguradas y el someterse un poco a la pr&aacute;ctica de "perfiles raciales" (racial profiling) sea el precio justo a pagar para parar el terrorismo. Esa es la justificaci&oacute;n popular para la cacer&iacute;a de brujas contra los inmigrantes detenidos--irrespectivo de la ret&oacute;rica legal detr&aacute;s de la que se esconden Ashcroft y su pandilla. </P>
<P>Pero con miles--la cifra total es desconocida--de hombres &aacute;rabes detenidos, y a ninguno le han formulado cargos de ning&uacute;n crimen relacionado con el 11 de septiembre, la justificaci&oacute;n se est&aacute; agotando. </P>
<P>Y lo que es m&aacute;s importante, la justificaci&oacute;n es m&aacute;s f&aacute;cil de cotorrear si uno no tiene familiares o amigos a los que se llevaron los federales hace meses atr&aacute;s por ninguna raz&oacute;n particular, y de quienes no se ha sabido nada desde entonces. </P>
<P>"Las detenciones y los secuestros en medio de la noche han creado un clima total de temor en las comunidades que han sido se&ntilde;aladas como blancos," dice Subash Kateel, de Desis Rising Up and Moving (DRUM), un grupo basado en Nueva York que ha organizado protestas para presionar al Servicio de Inmigraci&oacute;n y Naturalizaci&oacute;n (SIN). "Las detenciones son usadas por el gobierno para intimidar a estas comunidades tanto que no se atreven a denunciar p&uacute;blicamente este abuso."</P>
<P>Kateel y otros organizadores dicen que sus grupos reciben llamadas todo el tiempo de parientes de los detenidos--pero que los familiares y amigos est&aacute;n a menudo atemorizados de levan-tar su voz. Esta es una raz&oacute;n por la que las protestas contra las "desapariciones" de Ashcroft han sido peque&ntilde;as hasta ahora. </P>
<P>Pero esto ha comenzado a cambiar. En la ciudad de Nueva York, las demostraciones semanales afuera del Centro Metropolitano de Detenci&oacute;n en Brooklyn--donde los activistas creen que hay de 30 a 40 detenidos por la investigaci&oacute;n del 11 de septiembre--se han duplicado en tama&ntilde;o a unas 200 o m&aacute;s personas en las &uacute;ltimas semanas. </P>
<P>Aun m&aacute;s, dice Debbie Almontaser, una de las organizadoras de las protestas, las demostraciones se han ido tornando cada vez m&aacute;s multiraciales. "Poco a poco, m&aacute;s gente de otras comunidades ha estado llegando--afro-americanos, latinos, como tambi&eacute;n gente del sur de Asia y musulmanes", le dijo Almontaser a <I>Obrero Socialista</I>. "Esto demuestra que estos grupos han llegado a confiar en la comunidad en general. Ellos ven ahora que esa gente los apoya--que ya no est&aacute;n solos y que pueden salir a defender sus derechos."</P>
<P>Mientras tanto, DRUM ha organizado demostraciones en el Centro de Detenci&oacute;n del SIN en Passaic, N. J. El grupo ha hecho conexiones con algunos de los que est&aacute;n detenidos adentro--y ha conseguido que investigadores de los grupos Amnist&iacute;a Internacional y Human Rights Watch entren a las facilidades, cosa que se les hab&iacute;a prohibido antes por oficiales del SIN. En otras ciudades tambi&eacute;n hay activistas que est&aacute;n apoyando esta lucha. </P>
<P>Ashcroft y sus compa&ntilde;eros cazadores de brujas est&aacute;n explotando el miedo para as&iacute; usar de chivos expiatorios a una minor&iacute;a vulnerable. Pero su ataque contra unos pocos termina apuntando contra todos nosotros. No podemos dejar que se salga con la suya.</P>


