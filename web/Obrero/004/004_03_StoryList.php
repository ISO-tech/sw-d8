<P><font face="Times New Roman, Times, serif" size="5"><b>Am&eacute;rica Latina: &#191;Nueva frontera en la guerra de Bush?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">5 de abril de 2002 | P&aacute;gina 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>En marzo el presidente George Bush viaj&oacute; a M&eacute;xico, El Salvador y Per&uacute; para sermonear a los l&iacute;deres latinoamericanos sobre la corrupci&oacute;n y el mercado libre. Aunque hablaba de "la compasi&oacute;n" para reformar los mercados, Bush no predicaba con su propio ejemplo de generosidad.</P>
<P>Ofreci&oacute; la cantidad miserable de $5 mil millones de ayuda econ&oacute;mica a lo largo de tres a&ntilde;os--&#161;empezando en 2004! Esto para pa&iacute;ses como El Salvador y Guatemala, donde casi 500,000 pueden morirse de hambre a causa de la sequ&iacute;a, y para la Argentina, donde millones sufren de hambre y desempleo.</P>
<P>Mientras tanto, los EE.UU. buscan otras maneras--militares y econ&oacute;micas--de imponer su pol&iacute;tica neoliberal. Abajo analizamos las situaciones de Venezuela, Colombia y Puerto Rico.</P>
