<font face="Times New Roman, Times, serif" size="3">En lo que aparenta ser un ataque orquestado contra el presidente venezolano Hugo Ch&aacute;vez Fr&iacute;as, la prensa estadounidense y la dom&eacute;stica se han aliado con militares, los grandes negocios, la iglesia cat&oacute;lica, e incluso con altos oficiales de los sindicatos en protestas organizadas en las calles, en conferencias de prensa y en editoriales diarios exigiendo que el presidente renuncie a su cargo. </P>

