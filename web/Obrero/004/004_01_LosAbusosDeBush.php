<P><font face="Times New Roman, Times, serif" size="4">Tenemos que unir nuestras voces</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>&#161;No a los abusos de Bush!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">5 de abril de 2002 | P&aacute;gina 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GUERRA EN las Filipinas, en Colombia, en Irak; violaci&oacute;n de las libertades civiles de los inmigrantes, y recortes en los impuestos para los ricos--mientras que el resto de nosotros vemos nuestros servicios b&aacute;sicos y cosas tan importantes como el presupuesto escolar recortados, y nuestros empleos esfumarse.</P>
<P>George W. Bush se cree que puede hacer todo lo que le d&eacute; la gana. No importa cu&aacute;n injusto o desquiciado sea.</P>
<P>La opini&oacute;n del resto del mundo concuerda en que la Casa Blanca parece estar habitada por lun&aacute;ticos, luego de que el gobierno anunciara su nueva postura nuclear. En &eacute;sta anuncia que tiene una lista de pa&iacute;ses que son posibles blancos de ataques nucleares, no importa si ese pa&iacute;s posee o no bombas nucleares.</P>
<P>Aun figuras "respetables" de los EE.UU. han llegado a la conclusi&oacute;n de que a Bush se le ha pasado la mano. El columnista del <I>Boston Globe,</I> Robert Kuttner, escribi&oacute; recientemente "Si se trata del mal especificado eje del mal, o de la decisi&oacute;n de hacer la guerra nuclear t&aacute;ctica algo digno de consideraci&oacute;n, o un gobierno dom&eacute;stico "bajo las sombras," o de las divulgaciones de los planes para atacar a Irak, George W. Bush es a su manera tan atemorizante como al-Qaeda."</P>
<P>En el Medio Oriente Bush sigue apoyando incondicionalmente a raz&oacute;n de m&aacute;s de $4 mil millones anuales al genocidio del gobierno israel&iacute; contra el pueblo palestino. </P>
<P>En Colombia, sigue aumentando el dinero y las armas que manda al gobierno terrorista de Andr&eacute;s Pastrana para que su ej&eacute;rcito y sus socios psic&oacute;ticos paramilitares asesinen a m&aacute;s campesinos y activistas sindicales. </P>
<P>Y dom&eacute;sticamente intenta asfixiar la oposici&oacute;n a sus pol&iacute;ticas atacando a los sectores m&aacute;s vulnerables. Se estima que sobre 2,000 inmigrantes &aacute;rabes o musulmanes han sido detenidos por la administraci&oacute;n de Bush, y a s&oacute;lo uno se le han formulado cargos relacionados a los ataques del 11 de septiembre. Los dem&aacute;s han padecido humillaci&oacute;n, muchos bajo confinamiento solitario, y muchos otros est&aacute;n virtualmente desaparecidos para sus familias.</P>
<P>Mientras tanto, la Corte Suprema--quien le otorg&oacute; la presidencia a Bush en las elecciones robadas del 2000--decidi&oacute; recientemente que los inmigrantes indocumentados no tienen el derecho a la libre asociaci&oacute;n en sindicatos ni a protecci&oacute;n contra las violaciones de sus derechos laborales por parte de sus patronos.</P>
<P>De acuerdo a Arturo Rodr&iacute;guez, presidente del Sindicato de Trabajadores Agr&iacute;colas (UFW)--fundado por el legendario C&eacute;sar Ch&aacute;vez--"Esto elimina el derecho de los trabajadores a confrontar a sus patrones&#133;Esto los pone casi en condiciones de esclavitud, no teniendo los recursos para defenderse."</P>
<P>Por eso es necesario que todos los que estamos bajo ataque por las pol&iacute;ticas de Bush nos juntemos y levantemos nuestras voces para ponerle un alto a este abuso. Las protestas del fin de semana del 19 al 22 de abril en Washington, D.C. son un gran punto de partida para esto. </P>
<P>All&iacute; se juntar&aacute;n los que se oponen a la hip&oacute;crita "guerra contra el terrorismo" con quienes se oponen a que Colombia se convierta en el nuevo Vietnam, y con quienes se oponen a la avaricia corporativa y su globalizaci&oacute;n para los ricos, como tambi&eacute;n quienes apoyan la lucha del pueblo palestino.</P>
<P>&#161;Unamos nuestras luchas y juntemos nuestras voces!</P>

