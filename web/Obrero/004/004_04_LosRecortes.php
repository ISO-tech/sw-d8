<P><font face="Times New Roman, Times, serif" size="4">Estados y ciudades reducen servicios sociales esenciales</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Luchemos contra los recortes</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">5 de abril de 2002 | P&aacute;gina 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>Las autoridades pol&iacute;ticas de las ciudades y los estados a trav&eacute;s del pa&iacute;s est&aacute;n oyendo un mensaje fuerte y claro: "No estamos dispuestos a aceptar m&aacute;s rebajas al presupuesto social." As&iacute; cientos de estudiantes de los City Colleges de Chicago se lo dejaron saber al alcalde Richard Daley el jueves 21 de marzo, en una protesta contra los planes de eliminar los servicios de consejer&iacute;a acad&eacute;mica. </P>
<P>Diecinueve consejeros pueden perder sus puestos. Estos apoyan el desarrollo acad&eacute;mico de m&aacute;s de 70,000 estudiantes y son cruciales en gu&iacute;arlos en el proceso de transferencia hacia las universidades.</P>
<P>"Muchos estudiantes no toman las clases que realmente necesitan y terminan, como yo, permaneciendo m&aacute;s tiempo y pagando m&aacute;s dinero del que deber&iacute;an," dijo el estudiante Raven Cox, mientras los manifestantes, mayoritariamente latinos y afro-americanos, gritaban a los polic&iacute;as que les imped&iacute;an el paso a las oficinas de la alcald&iacute;a. "Creo que esto muestra que ellos no est&aacute;n interesados en la educaci&oacute;n, sino en construir m&aacute;s c&aacute;rceles y financiar m&aacute;s guerras."</P>
<P>Otro estudiante, Jonathan Pettis, agreg&oacute;: "El rico se hace m&aacute;s rico y el pobre m&aacute;s pobre. Si pueden mantenerte mentalmente esclavo, pueden controlarte, incluso si ya no tenemos grilletes ni cadenas. Nosotros, estudiantes afro-americanos no podemos dejarlos controlar qu&eacute; aprendemos, qu&eacute; leemos o qu&eacute; tan lejos nuestra educaci&oacute;n puede avanzar. Si lo hacen, no importa como este sistema se llame a s&iacute; mismo, yo lo llamo una dictadura."</P>
<P>El problema est&aacute; tomando un curso similar en ciudades y pueblos a trav&eacute;s del pa&iacute;s. En Boston, profesores y padres furiosos abarrotaron la reuni&oacute;n de la Junta Escolar, donde los administradores quer&iacute;an arrasar con un recorte presupuestario de $41 millones que pretend&iacute;a eliminar los pases de transportaci&oacute;n p&uacute;blica para los alumnos y despedir un n&uacute;mero de profesionales de la salud.</P>
<P>En Detroit, oficiales de la Junta Escolar planeaban tener su reuni&oacute;n del 20 de marzo a puertas cerradas para evitar tener la experiencia de la sesi&oacute;n anterior--cuando padres y miembros del sindicato interrumpieron los planes de eliminar 700 puestos de trabajo.</P>
<P>De hecho, 36 estados est&aacute;n, o planean, implementar recortes sociales para reducir el d&eacute;ficit presupuestario, el que podr&iacute;a alcanzar la suma de $100 mil millones el pr&oacute;ximo a&ntilde;o, de acuerdo al Centro de Pol&iacute;ticas y Prioridades Presupuestarias. Mucha de la culpa recae en George W. Bush, quien ha estado socavando el gasto social para pagar por los recortes en los impuestos de las corporaciones y de los ricos, y para sustentar el masivo gasto militar.</P>
<P>Republicanos y Dem&oacute;cratas est&aacute;n empujando por igual por estos recortes a nivel estatal y local, y despu&eacute;s de a&ntilde;os de ret&oacute;rica santurrona en Washington acerca de la importancia de la educaci&oacute;n, las escuelas p&uacute;blicas est&aacute;n sufriendo los m&aacute;s escandalosos recortes en sus presupuestos.</P>
<P>Dado que las protestas, marchas y asambleas contra estos recortes est&aacute;n a&uacute;n en su etapa inicial, adem&aacute;s del silencio de los medios, estas se&ntilde;ales de resistencia permanecen semi-ocultas, pero en cada una de ellas una l&iacute;nea com&uacute;n puede ser escuchada.</P>
<P>No toleraremos estos recortes que amenazan el futuro de nuestros hijos, y estamos listos para desafiarlos y pararlos.</P>


