<P><font face="Arial, Helvetica, sans-serif" size="2"><b>NUESTRA POSICI&Oacute;N</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">EE.UU. es responsable de la arremetida salvaje de Israel</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>El monstruo que Washington cre&oacute;</b></font> </P>
<p><font face="Arial, Helvetica, sans-serif" size="2">12 de abril de 2002</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>CUANDO UNOS periodistas le preguntaron a George. W. Bush acerca de la soluci&oacute;n al conflicto de Israel y Palestina el lunes pasado, &eacute;l balbuce&oacute;: "Tiene que haber un esfuerzo de la coalici&oacute;n en la regi&oacute;n enfocado contra la paz, digo, contra el terror, por la paz". </P>
<P>Bush fue m&aacute;s sincero la primera vez. La "coalici&oacute;n enfocada" que Washington quiere en el Medio Oriente realmente es para la guerra. </P>
<P>Despu&eacute;s de declarar "victoria" al restaurar a los pandilleros regionales en el poder en Afganist&aacute;n, el mes pasado Bush mand&oacute; al Vicepresidente Dick Cheney al Medio Oriente para recabar apoyo entre los gobiernos &aacute;rabes para la Fase Dos de la "guerra contra el terror" del gobierno de EE.UU. </P>
<P>El blanco: Irak. </P>
<P>Pero el ataque violento de Israel contra el pueblo palestino se le ha cruzado en el medio. No que Washington tenga inter&eacute;s en la justicia para los palestinos. La Casa Blanca ha denunciado repetidas veces a Yasser Arafat, mientras que el Primer Ministro Israel&iacute; Ariel Sharon incrementaba los ataques. </P>
<P>Mientras tanto, las monarqu&iacute;as y las dictaduras &aacute;rabes del Medio Oriente quieren que el "problema Palestino" desaparezca. Esto es por lo que apoyaron el "plan de paz" saudita para crear un estado palestino impotente, sin dar a los refugiados el derecho a regresar a sus tierras. </P>
<P>Pero la magnitud del asalto de Israel forz&oacute; a los gobiernos &aacute;rabes a denunciar a Israel. La lucha en Palestina es tan popular en los pa&iacute;ses &aacute;rabes e isl&aacute;micos porque est&aacute; en el coraz&oacute;n de la lucha contra el imperialismo en el Medio Oriente. </P>
<P>Esto es por lo que la pandilla de Bush ha vacilado en d&iacute;as recientes entre declarar que Israel tiene "derecho a defenderse" y apoyar la resoluci&oacute;n de las Naciones Unidas que hace un llamado a que las fuerzas israel&iacute;es se retiren del campamento de Yasser Arafat en Ramallah. </P>
<P>"Me parece que estoy confundido", dijo un periodista al secretario de prensa de la Casa Blanca Ari Fleischer en una la conferencia de prensa el lunes. "&#191;Cu&aacute;l es, queremos que Israel se retire, o apoyamos lo que Sharon est&aacute; haciendo?" </P>
<P>Washington est&aacute; dividido entre los que quieren permitir que Israel ahogue la resistencia palestina en sangre y otros que creen que es necesario revivir el "proceso de paz del Medio Oriente" antes de comenzar un proceso nuevo de guerra contra Irak.</P>
<P>Pero independientemente de los giros diarios, ambos lados en el debate de Washington quieren estar seguros de que los intereses de los EE.UU. est&aacute;n garantizados en el Medio Oriente. </P>
<P>Esto significa que Israel seguir&aacute; siendo el mayor recipiente de ayuda militar estadounidense, y que actuar&aacute; como sabueso de Washington en la regi&oacute;n. Si el perro se le escap&oacute; de la correa ahora, el amo s&oacute;lo tiene que culparse a &eacute;l mismo. </P>
<P>Adem&aacute;s, Israel s&oacute;lo est&aacute; aplicando el propio enfoque de Washington en la "guerra contra el terrorismo": usar el poder&iacute;o militar para imponer sus deseos, no importa las consecuencias. </P>
<P>La revista <I>New Yorker</I> public&oacute; un art&iacute;culo la semana pasada por Nicholas Lemann sobre "los halcones de la Casa Blanca y su nueva estrategia para la dominaci&oacute;n global", incluyendo el uso de armas nucleares t&aacute;cticas. </P>
<P>Los oficiales del gobierno consideran el 11 de septiembre como un "momento transformador", Lemann escribi&oacute;, porque "redujo dr&aacute;sticamente la resistencia usual del p&uacute;blico estadounidense al envolvimiento militar en otras tierras, por lo menos por un tiempo". </P>
<P>"&#191;Los Estados Unidos est&aacute; ahora en la posici&oacute;n de volver a trazar mapas regionales, especialmente en el Medio Oriente, y de reemplazar gobiernos por la fuerza? Nadie pens&oacute; que la administraci&oacute;n de Bush estar&iacute;a pensando en t&eacute;rminos tan ambiciosos, pero simplemente lo est&aacute;, y con el debate interno m&aacute;s a la derecha de d&oacute;nde estaba tan s&oacute;lo unos meses atr&aacute;s". </P>
<P>No podemos permitir que Bush pose de pacifista mientras que se prepara para una guerra total contra Irak, una naci&oacute;n todav&iacute;a en ruinas despu&eacute;s de la Guerra del Golfo de 1991 y de una d&eacute;cada de sanciones asesinas. </P>
<P>Las demostraciones contra la guerra del 19 al 22 de abril en Washington proporcionan una oportunidad importante para hacer estas conexiones. </P>
<P>La gente se ha tirado a las calles a trav&eacute;s de los EE.UU. escandalizados por la guerra de Israel contra los palestinos. </P>
<P>Estos pueden unirse al movimiento para parar la campa&ntilde;a de guerra de Washington&#150;junto con otros que ya han rechazado los esfuerzos de Bush de esparcir la guerra de los EE.UU. alrededor del mundo. </P>
<P>Necesitamos la asistencia m&aacute;s grande posible en Washington para exigir un alto a la ayuda de los EE.UU. a Israel&#150; y para decirle a Bush que no nos quedaremos cruzados de brazos mientras que los EE.UU. ampl&iacute;a su guerra.</P>

