<P><font face="Times New Roman, Times, serif" size="5"><b>&#161;Alto a los ataques contra inmigrantes!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">5 de abril de 2002 | P&aacute;gina 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>Las comunidades latinas, inmigrantes o nacidas aqu&iacute;, documentadas o indocumentadas, han sufrido bajo las leyes represivas y medidas tomadas desde el 11 de septiembre, dijo Cristian Ram&iacute;rez, director del cap&iacute;tulo de San Diego del American Friends Service Committee. "La Patrulla Fronteriza ha cambiado de estrategia", dijo Ram&iacute;rez, "de patrullar la frontera a patrullar las comunidades."</P>
<P>Dijo que los agentes de la Migra en San Diego ahora abordan el transporte p&uacute;blico, registran a los pasajeros, y deportan a algunos inmediatamente. "Hablamos no solo de inmigrantes indocumentados. Tenemos varias quejas de ciudadanos estadounidenses que son sacados del tranv&iacute;a porque los agentes no les creen."</P>
<P>Dijo que muchos en San Diego est&aacute;n ahora atemorizados de tomar el tranv&iacute;a para ir a trabajar o a la escuela. "Esto ha impactado a toda la comunidad latina. Nos dicen que la Patrulla Fronteriza trata de combatir el terrorismo. Nosotros decimos que la presencia de la Patrulla Fronteriza en el tranv&iacute;a crea terror."</P>
<P>Ram&iacute;rez tambi&eacute;n dijo que los registros m&aacute;s estrictos para los que cruzan la frontera han aumentado las demoras para los que viajan hacia y de sus trabajos. &Eacute;l indic&oacute; que esa gente ahora tiene m&aacute;s que temer de tales encuentros. </P>
<P>Bajo la ley USA PATRIOT, la gente ahora puede ser detenida desde cinco a siete d&iacute;as sin cargos. "Muchos son detenidos por infracciones de inmigraci&oacute;n," &eacute;l dijo, "y ahora mismo ellos se enfocan en gente con procedencia del Medio Oriente. Pero (desde la perspectiva de inmigrantes latinos) nos sentimos muy inc&oacute;modos."</P>
<P>Ram&iacute;rez dijo que la Patrulla Fronteriza, que tiene un record largo de brutalidad, est&aacute; en una campa&ntilde;a de reclutamiento para aumentar el n&uacute;mero de agentes. Y 4,000 efectivos de la Guardia Nacional van a ser desplegados a partir del 2 de abril a trav&eacute;s de San Diego, muchos en la frontera. </P>
<P>&Eacute;l tambi&eacute;n se&ntilde;al&oacute; la legislaci&oacute;n del Congreso aprobada el 3 de marzo, en que unos 250,000 ciudadanos mexicanos se naturalizar&aacute;n, mientras que $300 millones ser&aacute;n a&ntilde;adidos al presupuesto para la "seguridad fronteriza."</P>
<P>"Hemos visto esta clase de cosa antes. Ellos pasan una ley con algo bueno en ella, pero se empaqueta con un aumento en la represi&oacute;n."</P>


