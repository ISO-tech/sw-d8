<font face="Times New Roman, Times, serif" size="5"><b>Contradicciones del voto latino por Bush</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Por Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | 26 de noviembre de 2004 | p&aacute;gina 2</font><p>

<font face="Times New Roman, Times, serif" size="3">DOS TERCIOS de los latinos votaron por los Dem&oacute;cratas en el 2000, y en cambio este a&ntilde;o votaron por John Kerry por un margen de 55 por ciento. Este resultado representa una p&eacute;rdida de m&aacute;s de un mill&oacute;n de votos esperados por los Dem&oacute;cratas. Esto asusta a los Dem&oacute;cratas, que tradicionalmente se han cre&iacute;do de que el voto latino les pertenece.<p>
&iquest;C&oacute;mo se explica la disminuci&oacute;n del voto latino Dem&oacute;crata? <p>
La mayor&iacute;a de los comentaristas reclaman que la influencia de los "valores morales" conservadores en cuanto al derecho al aborto o al matrimonio gay impuls&oacute; a los latinos a votar por Bush. Otros dicen que los latinos, que representan una minor&iacute;a sustancial de las tropas en Irak, votaron por Bush por razones "patri&oacute;ticas".<p>
Ambas explicaciones cuentan con evidencia para sostenerse, pero de manera insuficiente. La pregunta real deber&iacute;a ser &iquest;por qu&eacute; los Dem&oacute;cratas no pudieron ganar los votos de una comunidad que ha sufrido por la falta de seguro m&eacute;dico m&aacute;s que otras comunidades, o cuyos jovenes han muerto en Irak con tasas m&aacute;s altas que las de los blancos y los afroamericanos?<p>
La respuesta es sencilla. Los Dem&oacute;cratas se cre&iacute;an que ten&iacute;an el voto latino en el bolsillo, sin tener que hacer nada para ganarlo.<p>
Kerry despleg&oacute; su campa&ntilde;a del "yo tambi&eacute;n" emulando a Bush en cuanto a los asuntos de la guerra en Irak, el terrorismo y otros, para atraer a los supuestos votantes indecisos. Sus propuestas con respecto al seguro m&eacute;dico, los empleos y la educaci&oacute;n fueron t&iacute;midas. Durante los debates con Bush, &iexcl;Kerry se atrevi&oacute; a criticar a Bush por no haber fortalecido las fronteras en contra de la inmigraci&oacute;n!<p>
Si los Dem&oacute;cratas no ofrecen argumentos para votar por ellos, no debe sorprendernos que los latinos o cualquier otro grupo voten por los Republicanos por otras razones--o que no voten por nadie.<p>
Por eso Nativo Lopez, un activista comunitario californiano de muchos a&ntilde;os, renunci&oacute; al Partido Dem&oacute;crata, se afili&oacute; al Partido Verde y apoy&oacute; la candidatura independiente de Ralph Nader y Peter Camejo. Aunque Nader y Camejo recibieron apenas 500,000 votos, los latinos votaron por ellos a un nivel dos veces mayor que el resto del electorado.<p>
"Nunca hemos recibido el apoyo leg&iacute;timo o sincero del partido [Dem&oacute;crata] que hemos ayudado a construir, y en muchos casos ni de los candidatos electos de este partido", dijo a <i>Obrero Socialista</i> Lopez, l&iacute;der de la Mexican American Political Association. "Es un partido que no responde a las necesidades de la mayor&iacute;a y es dirigido por millonarios blancos y sus consejeros pol&iacute;ticos que solamente tratan de imponer su voluntad pol&iacute;tica sobre la mayor&iacute;a (incluso los trabajadores anglosajones), y &uacute;nicamente apoyan a los candidatos minoritarios que pueden controlar y manipular para su propio inter&eacute;s econ&oacute;mico".<p>
Mientras m&aacute;s gente llegue a las mismas conclusiones que Lopez--y busque una alternativa de izquierda en vez de apoyar a los dos partidos del status quo--mejor ser&aacute; para el pueblo trabjador.<p>

