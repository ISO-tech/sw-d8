<font face="Times New Roman, Times, serif" size="5"><b>Carnicer&iacute;a en Faluya</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">26 de noviembre de 2004 | p&aacute;gina 4</font><p>

<font face="Times New Roman, Times, serif" size="3">GEORGE W. BUSH y los belicosos de Washington dicen que Faluya ha sido "liberada". As&iacute; que, &iquest;c&oacute;mo se supone que luzca la "liberaci&oacute;n"? <p>
"Hay cuerpos regados por las calles, y la mayor&iacute;a de las familias se han visto forzadas a enterrar a sus muertos en sus patios", Hamid Flewa, un residente de Faluya, dijo a la BBC. "Puedo ver filas de cuerpos a lo largo del pavimento".<p>
Hamid se siente forzado a preguntar por qu&eacute;. "No tenemos agua ni electricidad", dijo &eacute;l. "Estamos consumiendo nuestras provisiones muy r&aacute;pidamente. Y ahora ya no puede entrar m&aacute;s comida a la ciudad. Somos seres humanos. ..&iquest;Por qu&eacute; tenemos que pasar por esto?"<p>
Para Flewa y las otras v&iacute;ctimas de la ofensiva de EE.UU., la horrible respuesta es que son usados para mandar un mensaje al resto de Irak--que cualquiera dispuesto a confrontar la ocupaci&oacute;n estadounidense ser&aacute; aplastado. <p>
&iquest;Qu&eacute; de las decenas de miles de civiles inocentes atrapados en medio de las bombas de EE.UU., de sus morteros y sus balas? A Washington no le interesa ni pensar en sus vidas. Seg&uacute;n dijo despectivamente el Secretario de Defensa Donald Rumsfeld, "Los civiles inocentes en esa ciudad han sido instru&iacute;dos adecuadamente en cuanto a c&oacute;mo poder evitar el meterse en l&iacute;os". <p>
Que le cuenten eso a Muna Salim--cuya hermana embarazada Artica y siete otros miembros de la familia murieron cuando dos cohetes estadounidenses destruyeron su hogar. "No puedo sacar la imagen de mi mente de c&oacute;mo su feto vol&oacute; fuera de su cuerpo", Muna dijo al peri&oacute;dico <i>Scottish Sunday Herald</i>. &iquest;"Por qu&eacute; fue nuestra familia bombardeada? Nunca hubo combatientes en nuestra area".<p>
Pero a Bush y su pandilla no le importan estas cuestiones. S&oacute;lo les interesa "apaciguar" a Irak antes de las elecciones pautadas para enero. Eso significa usar las armas m&aacute;s letales en el arsenal del Pent&aacute;gono--y agitar a sus soldados para que hagan lo inconcebible. <p>
Como cuando un <i>marine</i> estadounidense fue filmado matando un a preso iraqu&iacute;, que estaba herido y desarmado, en una de las mezquitas de Faluya. El corresponsal de la NBC Kevin Sites dijo que varios presos heridos hab&iacute;an sido dejados en la mezquita para luego ser llevados a recibir tratamiento m&eacute;dico. Pero cuando un grupo de <i>marines</i> entr&oacute; y los encontr&oacute; all&iacute;, uno vio a un preso que respiraba. "&Eacute;l jodido pretende estar muerto", grit&oacute; el <i>marine</i>. <p>
"El <i>marine</i> entonces levanta su rifle y le dispara al hombre en la cabeza", cont&oacute; Sites. "Los retratos son demasiado fuertes por lo que no podemos transmitirlos".<p>
Son horrores como estos lo que explica por qu&eacute; EE.UU. puede ser capaz de ganar batallas como estas usando un poder&iacute;o abrumador, pero no significa que est&eacute; ganado la guerra--pues n&uacute;meros mayores de iraqu&iacute;es comunes se van uniendo a la resistencia dado el odio que se ha ganado EE.UU. y su gobierno de titeres iraqu&iacute;es. <p>
Cada d&iacute;a que EE.UU. controla a Irak produce m&aacute;s atrocidades, cr&iacute;menes de guerra y la opresi&oacute;n de todo un pueblo. &iexcl;Tenemos que exigir un alto a la ocupaci&oacute;n ahora!<p>

