<font face="Times New Roman, Times, serif" size="4">Contra los ataques de Bush estamos...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>En pie de lucha</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">26 de noviembre de 2004 | p&aacute;gina 1</font><p>

<font face="Times New Roman, Times, serif" size="3">GEORGE W. BUSH se est&aacute; preparando para lanzar ataques--con planes para nuevas guerras, privatizar el sistema de Seguro Social, y proscribir el derecho al aborto de la mujer. Y no va a detenerse hasta que sea confrontado por una resistencia popular.<p>
Los ayudantes de Bush no han perdido tiempo en revelar a los medios sus esquemas m&aacute;s exagerados: privatizar el Seguro Social, reemplazar el sistema de contribuciones sobre ingresos con un impuesto nacional sobre las ventas que es regresivo (afecta m&aacute;s a los pobres que a los ricos), restringir el derecho de los consumidores a demandar en las cortes a las corporaciones.<p>
En el &aacute;mbito internacional, la agresi&oacute;n de Bush va a aumentar, desde el aniquilamiento de la resistencia iraqu&iacute; en Faluya, hasta el aumento de asistencia militar al gobierno colombiano para su guerra sucia contra la guerrilla.<p>
A la vez, Bush promete imponer "disciplina en los gastos"--jerga con que indica su deseo de desangrar lo que queda de la red de seguridad social. Quiz&aacute;s lo m&aacute;s pol&eacute;mico ser&aacute; cuando Bush intente premiar a los fundamentalistas cristianos con el nombramiento de jueces federales, incluso jueces de la Corte Suprema, que se oponen al derecho al aborto.<p>
Si Bush est&aacute; en la posici&oacute;n empujar tal agenda, se lo debe a los Dem&oacute;cratas porque no se puede contar con ellos para ofrecer ninguna resistencia real. Los Dem&oacute;cratas fueron quienes le dieron a Bush los votos que necesitaba para aprobar sus recortes de impuestos para los ricos en el Senado--y la campa&ntilde;a terrible del Senador John Kerry nunca desafi&oacute; a Bush en cuanto a la guerra en Irak o sobre cualquier otro asunto de mayor importancia.<p>
Sin embargo, podemos detener a Bush--por medio de la organizaci&oacute;n, la protesta y la lucha. Hoy en d&iacute;a, la guerra de Bush en Irak todav&iacute;a es detestada por muchos--y debe convertirse en el foco de nuevas protestas. Los sindicatos, las organizaciones comunitarias y otras tienen que convocar una oposici&oacute;n ahora para derrotar la tentativa de Bush de destruir al Seguro Social. Tenemos que renovar el movimiento por los derechos de la mujer--y enviar un mensaje claro de que lucharemos por el derecho al aborto sin disculpas porque la mujer tiene el derecho a controlar su propio cuerpo.<p>
Con respecto a estos asuntos no estamos partiendo de cero. Los cientos de miles de personas que participaron en las enormes protestas contra la guerra durante los &uacute;ltimos dos a&ntilde;os--y los millones m&aacute;s que se oponen a la guerra de Bush por petr&oacute;leo--pueden formar la base de una oposici&oacute;n real a la agenda imperial de Washington. Y mientras m&aacute;s fuerte sea la resistencia contra Bush en cuanto a la guerra, m&aacute;s f&aacute;cil ser&aacute; nuestra capacidad para luchar por otros objetivos tambi&eacute;n.<p>
Bush tiene un exceso de confianza--y va a encontrarse en medio de peleas que no esperaba. Lo podemos hacer retroceder--y proseguir a luchar por nuestras propias prioridades.<p>

