<font face="Arial, Helvetica, sans-serif" size="2"><b>GOBERNADOR DE PUERTO RICO</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Resultado electoral cuestionado</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Por Roberto Barreto</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | 26 de noviembre de 2004 | p&aacute;gina 3</font><p>

<font face="Times New Roman, Times, serif" size="3">SEG&Uacute;N LOS resultados preliminares, en las elecciones de noviembre el ex-gobernador Pedro Rosell&oacute; perdi&oacute; la carrera a la gobernaci&oacute;n ante el candidato del Partido Popular Democr&aacute;tico (PPD), An&iacute;bal Acevedo Vil&aacute;. Sin embargo, los resultados finales de las elecciones est&aacute;n cuestionados. No solo se hace un recuento, sino que se debate en los tribunales la validez de las papeletas mixtas (con votos para candidatos de distintos partidos).<p>
Ambas c&aacute;maras legislativas y muchas de las alcald&iacute;as quedaron en manos del Partido Nuevo Progresista (PNP). A la vez, el Partido Independentista Puertorrique&ntilde;o (PIP) no obtuvo suficientes votos para permanecer inscrito como partido y ha reiniciado el proceso de recoger firmas para recobrar su franquicia electoral. <p>
En general, los resultados de las elecciones apuntan a un amplio rechazo a la gesti&oacute;n de la pasada administraci&oacute;n PPD que prometi&oacute; reducir la pobreza, el desempleo, la violencia y la corrupci&oacute;n pero fracas&oacute; miserablemente. La administraci&oacute;n de Sila Calder&oacute;n, continu&oacute; los operativos policiacos contra las comunidades pobres, atac&oacute; los sindicatos y calladamente adelant&oacute; la pol&iacute;tica de privatizaciones.<p>
La victoria legislativa del PNP se monta sobre este sentimiento anti-incumbente. La notable excepci&oacute;n lo fue su candidato a gobernador, Pedro Rosell&oacute;, a quien el pueblo no perdona por haber dirigido el gobierno m&aacute;s corrupto del que se tenga memoria, entre los a&ntilde;os 1993-2000. Miles de electores del PNP, y del PIP votaron mixto para favorecer a Acevedo Vil&aacute; y as&iacute; impedir una victoria de Rosell&oacute;. <p>
El PIP por su parte corri&oacute; una campa&ntilde;a en que no hizo menci&oacute;n alguna del independentismo. Llamaron a un voto protesta contra la inefectividad de los partidos principales que han gobernado en las &uacute;ltimas cuatro d&eacute;cadas sin resolver los principales problemas del pa&iacute;s. El PIP insisti&oacute; que podr&iacute;a administrar la colonia mejor que los partidos colonialistas. Al final miles de pipiolos desoyeron las directrices de su propio partido y votaron por el PPD o por su candidato a gobernador.<p>
En general los electores dan muestras de disgusto con los partidos pol&iacute;ticos tradicionales. Siete nuevos partidos regionales fueron inscritos en estas elecciones. La participaci&oacute;n electoral ha ido disminuyendo gradualmente, aunque contin&uacute;a alta. En 1984 vot&oacute; el 88.9 por ciento de los electores, mientras que en estas elecciones particip&oacute; el 80.9 por ciento.<p>
Gane quien gane no tendr&aacute; grandes repercusiones en la vida de los puertorrique&ntilde;os, pues las plataformas del PPD y el PNP son casi id&eacute;nticas.<p>

