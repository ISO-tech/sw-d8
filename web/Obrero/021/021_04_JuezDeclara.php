<font face="Times New Roman, Times, serif" size="5"><b>Juez declara guerras de EE.UU. ilegales</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Por H&eacute;ctor Reyes</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | 20 de mayo de 2005 | p&aacute;gina 4</font><p>

<font face="Times New Roman, Times, serif" size="3">El contramaestre de tercera clase Pablo Paredes obtuvo una victoria inesperada en el juicio que la Marina de EE.UU. hab&iacute;a entablado contra &eacute;l por haberse rehusado a abordar el buque Bonhomme Richard que parti&oacute; de San Diego el pasado diciembre para llevar tropas frescas a Irak.<p>
Paredes se hab&iacute;a rehusado a ser c&oacute;mplice de la guerra de Bush en Irak, y dijo que prefer&iacute;a ir a una prisi&oacute;n militar antes de llevar a <i>Marines</i> j&oacute;venes a morir, ser mutilados o a matar a gente inocente en Irak.<p>
Paredes hab&iacute;a sometido su solicitud como objetor de conciencia, pero la Marina procedi&oacute; a enjuiciarlo como desertor. En apoyo a Pablo, una campa&ntilde;a nacional de voluntarios se tir&oacute; a las calles recogiendo firmas, y hablando en iglesias, centros comunales, etc.<p>
La presi&oacute;n popular generada, y el odio tan grande a la guerra que se est&aacute; generando en el pueblo explican la decisi&oacute;n del juez naval, el Teniente Comandante Robert Klant, quien no le impuso ninguna condena de c&aacute;rcel a Paredes.<p>
Klant desestim&oacute; los cargos que equival&iacute;an a deserci&oacute;n y s&oacute;lo lo penaliz&oacute; por haber concientemente evadido la partida de su buque, por lo que lo sentenci&oacute; a dos meses de movimientos restringidos, a tres de trabajo forzado, y a una reducci&oacute;n de rango. Asombrosamente, el juez Klant admiti&oacute; que "Creo que el gobierno ha probado exitosamente que cualquier miembro del servicio militar tiene causas razonables para creer que las guerras de Yugoslavia, Afganist&aacute;n e Irak eran ilegales".<p>
La lucha de Pablo, como la de otros soldados que se han rehusado p&uacute;blica y valientemente a participar en la guerra--gente como Camilo Mej&iacute;a, y el Sargento Kevin Benderman, quien tambi&eacute;n est&aacute; enfrentando actualmente cargos militares--es una parte muy importante del movimiento contra la guerra. Esta victoria debe fortalecer nuestros esfuerzos para acabar con la ocupaci&oacute;n de Irak y hacer que las tropas de EE.UU. regresen a casa inmediatamente.<p>


