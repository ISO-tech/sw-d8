<font face="Times New Roman, Times, serif" size="5"><b>&iquest;C&oacute;mo podemos pararlos?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">20 de mayo de 2005 | p&aacute;gina 2</font><p>

<font face="Times New Roman, Times, serif" size="3">GEORGE BUSH y los Republicanos creen que pueden hacer lo que les d&eacute; la gana. Desde la monta&ntilde;a de regalos de cabilderos apilada por Tom Delay, l&iacute;der de la mayor&iacute;a republicana en el Congreso, hasta la nominaci&oacute;n del 's&uacute;per halc&oacute;n' John Bolton al puesto de embajador ante las Naciones Unidas y la determinaci&oacute;n de los Republicanos de atestar las cortes con jueces reaccionarios, Bush y sus aliados est&aacute;n inflados de confianza.<p>
El 24 de abril, un mensaje en video del l&iacute;der de la mayor&iacute;a del Senado, Bill Frist, Republicano de Tennessee, fue destacado en el programa televisivo titulado "Domingo de justicia: Alto al bloqueo legislativo contra los religiosos", auspiciado por el Family Research Council (Concilio pro-investigaci&oacute;n de la familia, FRC por sus siglas en ingl&eacute;s).<p>
La derecha religiosa organiz&oacute; el evento para oponerse a las amenazas de los Dem&oacute;cratas de usar las t&aacute;cticas del filibusterismo--un boicot u obstruccionismo parlamentario en que tan s&oacute;lo un senador puede impedir los procedimientos deliberativos al mantener la palabra indefinidamente--para bloquear la nominaci&oacute;n de candidatos derechistas a la judicatura federal. Frist dijo que &eacute;l est&aacute; dispuesto a revisar las reglas del Senado para impedir este tipo del bloqueo en cuanto a las selecciones judiciales--una maniobra que ha sido designada la 'opci&oacute;n nuclear' porque revocar&iacute;a m&aacute;s de 200 a&ntilde;os de precedentes en el Senado.<p>
La designaci&oacute;n de John Bolton como candidato al puesto de embajador de EE.UU. ante la ONU tambi&eacute;n est&aacute; en la lista de deseos de Bush.<p>
Bolton es una selecci&oacute;n rara porque parece que &eacute;l crea que la ONU no debiera existir. Bolton es un veterano halc&oacute;n que sirvi&oacute; en las administraciones de los presidentes Reagan y Bush, padre, y que abog&oacute; por que EE.UU. suspendiera sus pagos de cuotas a la ONU. La administraci&oacute;n de Bush quiere garantizar que la ONU contin&uacute;e si&eacute;ndole servil--y si, no que, pague el precio. Y Bolton es la clase de persona que puede lograrlo. <p>
Es obvio que hay una guerra en Washington. Desgraciadamente, s&oacute;lo uno de los bandos est&aacute; tirando golpes.<p>
Hasta ahora, los Dem&oacute;cratas en el Congreso han hecho poco para oponerse a la agenda Republicana. Muchos de ellos desertaron para apoyar la "reforma" de las leyes que controlan las bancarrotas personales, en realidad el "sue&ntilde;o dorado" de las compa&ntilde;&iacute;as de tarjetas de cr&eacute;dito, y por raras excepciones, la mayor&iacute;a vot&oacute; a favor del proyecto de ley que autorizaba otros $81 mil millones m&aacute;s para sufragar las ocupaciones de Irak y Afganist&aacute;n. Solamente dos senadores Dem&oacute;cratas votaron en contra del nombramiento de John Negroponte--quien organiz&oacute; las guerras genocidas de Washington contra el pueblo centroamericano en la d&eacute;cada de 1980--como director nacional de inteligencia.<p>
Respondiendo a la amenaza de los Republicanos de utilizar la 'opci&oacute;n nuclear' en el Senado, el portavoz Dem&oacute;crata del Senado, Harry Reid, se&ntilde;al&oacute; pusil&aacute;nimemente que "el Congreso ya ha aprobado 205 de los candidatos judiciales del presidente. Aparentemente, una tasa de aprobaci&oacute;n de 95 por ciento no es suficiente para este presidente". <p>
Si vamos a ver una real oposici&oacute;n a tener una monta&ntilde;a de fan&aacute;ticos anti-abortos y de amantes de la guerra en las cortes, en el Congreso y  en la Casa Blanca, tenemos que organizarla desde abajo, para decirles a los Republicanos y los Dem&oacute;cratas que no vamos a rendir nuestros derechos.<p>

