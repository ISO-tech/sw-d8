<font face="Times New Roman, Times, serif" size="5"><b>Defendamos a los inmigrantes</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Por Jeff Bale</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | 20 de mayo de 2005 | p&aacute;gina 1</font><p>

<font face="Times New Roman, Times, serif" size="3">La ret&oacute;rica racista de los vigilantes anti-inmigrantes lleg&oacute; a su l&oacute;gica consecuencia a fines de abril en una carretera de Arizona.<p>
Patrick Haab, un soldado reservista de 24 a&ntilde;os y veterano de la guerra de Irak se detuvo en un &aacute;rea de descanso de la autopista interestatal n&uacute;mero 8, al sur de Arizona. All&iacute; vio siete hombres latinos e inmediatamente pens&oacute; que eran "ilegales". Sac&oacute; su pistola "Glock", mand&oacute; a los siete a tirarse al pavimento, y llam&oacute; a la polic&iacute;a.<p>
Afortunadamente los siete no eran ilegales, apesar de las presunciones de Haab, cuando lleg&oacute; la polic&iacute;a--con la Patrulla Fronteriza--&eacute;sta termin&oacute; arrest&aacute;ndolo.<p>
Pero Haab no ser&aacute; procesado por su delito. Las autoridades del condado de Maricopa, AZ, decidieron no formularle cargos--porque seg&uacute;n la ley del estado uno puede hacer un "arresto ciudadano" si sospecha que lo hace contra un supuesto criminal cuya fechor&iacute;a pueda ser catalogada como felon&iacute;a.<p>
Este incidente sucedi&oacute; mientras cientos de vigilantes armados merodeaban la frontera entre Arizona y Sonora, M&eacute;xico. Aunque tomaron el nombre "Minutemen" en ingl&eacute;s, el nombre que la prensa mexicana les ha puesto explica mucho mejor quienes son: los cazamigrantes.<p>
Aunque los grupos latinos y los Dem&oacute;cratas no hicieron mucho en contra de los cazamigrantes, no pudieron ignorar lo que hizo Haab. Por fin organizaron conferencias de prensa, en las cuales prometieron pedirle al gobierno federal que enjuicie a Haab en la corte federal.<p>
Ciertamente Haab merece parar en la c&aacute;rcel. Sin embargo, se necesita mucho m&aacute;s que ret&oacute;rica sobre juicios federales para combatir efectivamente la ola anti-inmigrante. Aunque Arizona se ha convertido en la cabeza de playa de las nuevas batallas contra los inmigrantes, la verdad es que un nuevo movimiento anti-inmigrante est&aacute; creciendo por todo el pa&iacute;s.<p>
El gobernador de California, Arnold Schwarzenneger--quien es un inmigrante--proclam&oacute; en una entrevista radial que piensa que es "injusto, que hay tantas personas que entran [al pa&iacute;s], que tenemos fronteras abiertas". Lo que caus&oacute; tanto revuelo que tuvo que retractar sus palabras el d&iacute;a siguiente.<p>
Y dos l&iacute;deres de los cazamigrantes estuvieron en Washington durante la &uacute;ltima semana de abril, como invitados de honor en una conferencia de prensa en el Capitolio. Pidieron al Presidente Bush que mandara la Guardia Nacional para que ellos pudieran irse de la frontera. <p>
La aparente fortaleza de los activistas anti-inmigrantes no es porque representen una nueva mayor&iacute;a de la poblaci&oacute;n estadounidense que comparte sus ideas extremas en contra de los inmigrantes, sino que se debe a la profunda debilidad del bando que pretende defender los derechos de los inmigrantes.<p>
En mayo, el congreso aprob&oacute; un nuevo proyecto de ley que se llama el Real ID Act, la cual hace m&aacute;s dif&iacute;cil para inmigrantes obtener licencias de conducir atrav&eacute;s del pa&iacute;s. No s&oacute;lo fue solapadamente agregada esta provisi&oacute;n al presupuesto militar, a&uacute;n m&aacute;s ofensivo fue el apoyo total recibi&oacute; de los Democr&aacute;tas.<p>
En este clima anti-inmigrante que domina el quehacer pol&iacute;tico en Washington, la propuesta de reforma migratoria bipartisana presentada por los senadores McCain y Kennedy ha sido recibida como "generosa" por activistas para los derechos de los inmigrantes. <p>
Pero el proyecto no hace m&aacute;s que hacer acceptables las mismas ideas que Bush presentar&aacute; el a&ntilde;o pasado y que re-plantean las presunciones b&aacute;sicas del infame programa Bracero de hace 40 a&ntilde;os: suficientemente buenos para trabajar para los americanos, no para vivir entre ellos.<p>
Ha llegado la hora de rejuvenecer el movimiento pro-inmigrantes, para que confronte a los activistas anti-inmigrantes y al racismo que gu&iacute;a sus acciones. Necesitamos un movimiento que luche por un mundo sin fronteras.<p>

