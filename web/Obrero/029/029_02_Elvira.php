<font face="Times New Roman, Times, serif" size="3">AL CIERRE de este n&uacute;mero, la activista e indocumentada Elvira Arellano se refugiaba en una iglesia en Chicago para resistir su deportaci&oacute;n.<p>
En vez de presentarse a tu cita de deportaci&oacute;n programada para el 15 de agosto, Elvira decidi&oacute; refugiarse en la iglesia Metodista Unida Adalberto en el barrio latino de Humboldt Park. Cientos de simpatizantes ya han venido a la iglesia para manifestar su apoyo por Elvira y su lucha.<p>
En 2002, Elvira, una trabajadora de limpieza en el aeropuerto O'Hare de Chicago, fue arrestada durante una redada. Fue acusada de usar un n&uacute;mero de Seguridad Social falso. Agentes con armas en la mano irrumpieron en su hogar durante la madrugada y arrestaron a Elvira frente a su hijo.<p>
Elvira es madre soltera de Sa&uacute;l, que tiene siete a&ntilde;os y es ciudadano estadounidense. Si Elvira es deportada, se separar&aacute; de Sa&uacute;l, quien necesita tratamiento m&eacute;dico que solamente puede recibir en EEUU.<p>
Desde su arresto, Elvira se ha hecho una activista por los derechos de los inmigrantes, enfatizando la situaci&oacute;n de familias mezcladas de ciudadanos y no ciudadanos. Qu&eacute; puedes hacer:<p>
-- Haz aprobar resoluciones a favor de Elvira y en contra de las redadas y deportaciones en tu sindicato, iglesia y en las organizaciones comunitarias. Manda mensajes de solidaridad a Elvira a la iglesia Metodista Unida Adalberto, 2716 W. Division Street, Chicago, IL 60622.<p>
-- Exige a los congresistas y oficiales federales que terminen las redadas y deportaciones, en general, y demanda a los senadores Dick Durbin (312-353-4952) y Barack Obama (312-386-3506) que apoyen el bill HR 1628, que permitir&iacute;a a Elvira permanecer en el pa&iacute;s.<p>
-- Pide a tu iglesia, centro sindical y ciudad que se declare un santuario por los indocumentados.<p>
-- &Uacute;nete a las manifestaciones y otras acciones en apoyo de los derechos de los inmigrantes durante el fin de semana del D&iacute;a del Trabajo (Labor Day) y del 30 de septiembre al 6 de octubre.<p>

