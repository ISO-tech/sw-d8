<font face="Times New Roman, Times, serif" size="5"><b>Buscados por cr&iacute;menes de guerra</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">septiembre-octubre de 2006 | p&aacute;gina 1</font><p>

<font face="Times New Roman, Times, serif" size="3">EL ATAQUE israel&iacute; sobre el pueblo liban&eacute;s fue una guerra que el gobierno de EEUU quer&iacute;a y deseaba. Fue una escalada en la guerra que Israel lleva a cabo contra el pueblo palestino, con miras a avanzar y extenderse en la dominaci&oacute;n de los EEUU e Israel en el Medio Oriente.<p>
Los EEUU cargan con la responsabilidad por la muerte y destrucci&oacute;n causada por las fuerzas armadas de Israel; la matanza de cientos de personas, el desplazamiento de cientos de miles, y el bombardeo de puentes, usinas el&eacute;ctricas, f&aacute;bricas y caminos, operativo planeado para "hacer retroceder al L&iacute;bano 20 a&ntilde;os en el tiempo" como lo afirm&oacute; el Jefe del Estado Mayor del ejercito israel&iacute;, Dan Halutz.<p>
Israel a creado de forma deliberada una vasta crisis humanitaria, al desbastar la infraestructura del L&iacute;bano provocando una aguda escasez de alimentos, medicinas, alojamiento y combustibles.<p>
La masacre en Qana--que mat&oacute; a m&aacute;s de 60 libaneses, incluyendo familias enteras--sac&oacute; a la luz los fines isreal&iacute;es reales. Este bombardeo de no combatientes, que no respondi&oacute; a ning&uacute;n ataque por cohete de L&iacute;bano, fue una acci&oacute;n del terrorismo del estado israel&iacute;. Tanto el presidente Bush y Condoleeza Rice, su secretaria del Estado, como Ehud Olmert, el primer ministro de Israel, tienen las manos sangrientas.<p>
Israel afirm&oacute; que su asalto contra L&iacute;bano respondi&oacute; a la captura de dos soldados israel&iacute;es por parte de los combatientes de Hizbul&aacute;. Por el contrario, fue una campa&ntilde;a orquestada con mucha antelaci&oacute;n, y fue aprobada en lo pol&iacute;tico y equipada militarmente por los EEUU en el marco de las actuales guerras en Irak y Afganist&aacute;n.<p>
Como activistas en EEUU, tenemos la particular responsabilidad de apoyar al pueblo palestino y liban&eacute;s, y a todos aquellos que resisten las guerras de Israel y su apoyo norteamericano. Rechazamos el pretexto de la "guerra contra el terrorismo" como afirman tanto los Dem&oacute;cratas como los Republicanos, como asimismo tambi&eacute;n los medios de informaci&oacute;n del sistema.<p>
La matanza y la crisis humanitaria causadas por las bombas, los misiles y aviones israel&iacute;es de fabricaci&oacute;n norteamericana en la franja de Gaza y Cisjordania (West Bank) as&iacute; como en el L&iacute;bano, mostraron claramente la mentira de las promesas de democratizaci&oacute;n que acompa&ntilde;aron las invasiones norteamericanas en Irak y Afganist&aacute;n.<p>
Pero la escala de destrucci&oacute;n infligida al L&iacute;bano conlleva tambi&eacute;n un profundo mensaje a todo el mundo: que tanto EEUU como su ejecutor Israel est&aacute;n listos para usar los m&aacute;s terribles y sanguinarios m&eacute;todos a fin de lograr sus objetivos. El cese de fuego actual meramente pospone la pr&oacute;xima fase de la guerra norteamericana/isreal&iacute;.<p>
El apoyo total que recibe Israel y su guerra de agresi&oacute;n por parte de los pol&iacute;ticos y los medios, no solo refleja la alianza con Israel de larga data, sino el incremento del racismo y la represi&oacute;n por parte del estado hacia los &aacute;rabes y musulmanes en los EEUU<p>
Desde los ataques ocurridos el 11 de setiembre de 2001, EEUU ha usado la "guerra contra el terrorismo" para restringir las libertades civiles. Considerar a una persona m&aacute;s sospechosa por pertenecer a determinado grupo &eacute;tnico, interrogatorios, detenciones y deportaciones se han convertido en una realidad tanto para &aacute;rabes como para musulmanes.<p>
Este racismo converge con las ideas y suposiciones del proyecto sionista, que los &aacute;rabes y musulmanes no tienen derechos que valga la pena considerar.<p>
La guerra de Israel contra el L&iacute;bano es el presagio de un funesto nuevo giro de la "guerra contra el terrorismo" de Washington. Oponerse a los planes imperiales del gobierno norteamericano es de suma importancia para revivir y fortalecer el movimiento contra la guerra en los EEUU, desarrollando la solidaridad internacional y oponi&eacute;ndose a las intervenciones imperialistas norteamericanas, ya sean llevadas a cabo directamente por EEUU o por medio de los militares israel&iacute;es.<p>

