<font face="Arial, Helvetica, sans-serif" size="2"><b>EDITORIAL</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bush en el Medio Oriente: una hip&oacute;crita "Gira de la Paz"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">febrero-marzo de 2008 | p&aacute;gina 2</font><p>

<font face="Times New Roman, Times, serif" size="3">EN ENERO, el<i> New York Times</i> reprendi&oacute; a George Bush por esperar siete a&ntilde;os para abordar el delicado problema de intermediar una paz en el conflicto Palestino-Israel&iacute;. Al final, su p&aacute;gina editorial toc&oacute; su nota de esperanza.<p>
"El Sr. Bush dice que presionar&aacute; ambas partes cuando sea necesario, y tambi&eacute;n prometi&oacute; regresar al &aacute;rea en mayo para el 60&deg; aniversario de Israel--y tal vez m&aacute;s a menudo si fuera necesario" se lee en la editorial del <i>Times</i>. "Esperamos que esto significa que el presidente est&aacute; definitiva y verdaderamente comprometido".<p>
Pero esta imagen de constructor de paz tiene muy poco que ver con el verdadero prop&oacute;sito de la gira de Bush en el Medio Oriente: otorgar m&aacute;s apoyo a la pol&iacute;tica de sumisi&oacute;n o hambre del primer ministro israel&iacute; Ehud Olmert contra los palestinos bajo control de Hamas en Gaza, apuntalar el poder del presidente de la Autoridad Palestina Mahmoud Abbas en el mini-estado de Cisjordania, y acarrear apoyo entre los reg&iacute;menes &aacute;rabes para su campa&ntilde;a contra Ir&aacute;n.<p>
Bush reiter&oacute; su promesa de proveer una nueva ayuda econ&oacute;mica y militar a Israel de $30 mil millones en los pr&oacute;ximos 10 a&ntilde;os, un incremento de m&aacute;s del 25 por ciento.<p>
Esta contribuci&oacute;n ocurre aun cuando Israel planea ampliar dos asentamientos en Cisjordania en directo desaf&iacute;o al mapa de negociaciones respaldado por el mismo EEUU. El anuncio vino apenas horas despu&eacute;s que la Conferencia de Paz de Annapolis, convocada por Bush en noviembre, concluyera.<p>
Bush dice que los palestinos necesitan m&aacute;s que un quebrantado estado lleno de retenes, muros y asentamientos ilegales. Pero tambi&eacute;n dijo que s&oacute;lo los asentamientos que albergan a menos del 20 por ciento de los colonos en Cisjordania deber&aacute;n ser removidos, lo que deja intacta la estructura segregacionista de Israel.<p>
Bush afirm&oacute; adem&aacute;s que Israel debe seguir siendo "un estado jud&iacute;o", y recul&oacute; de su anterior promesa de establecer un estado palestino para finales del 2008.<p>
Luego, en una reuni&oacute;n a puerta cerrada, Bush y Olmert compararon notas sobre la "amenaza" representada por Ir&aacute;n--a pesar del hecho de que el Estimado Nacional de Inteligencia (NIE), un informe elaborado entre los 16 organismos de espionaje estadounidenses, lleg&oacute; a la conclusi&oacute;n de que Ir&aacute;n no representa una amenaza inminente y que detuvo su programa nuclear en el 2003, lo mismo que los funcionarios iran&iacute;es hab&iacute;an dicho.<p>
La difusi&oacute;n de la cantinela acerca de la amenaza iran&iacute; fue un objetivo central de la gira de Bush, de acuerdo con Phyllis Bennis, analista del Oriente Medio en el Instituto de Estudios Pol&iacute;ticos.<p>
"El agasajo de esta semana en el Medio Oriente es en parte una repetici&oacute;n de la homil&iacute;a de Annapolis: a pesar de la NIE, la administraci&oacute;n Bush sigue diciendo a los reg&iacute;menes &aacute;rabes, permanezcan con nosotros y movil&iacute;cense contra Ir&aacute;n, que seguiremos apoyando a ustedes con nueva ayuda militar", escribi&oacute; Bennis. "Y ustedes pueden mantener sus desasosegadas poblaciones en l&iacute;nea dici&eacute;ndoles que nosotros apoyamos un estado palestino."<p>
Para ello, Bush hizo paradas en Arabia Saudita, los Emiratos &Aacute;rabes Unidos, Kuwait, Bahrein y Egipto, los cuales todos son dictaduras o monarqu&iacute;as, aliados de Washington, con bases de tropas estadounidenses en su territorio.<p>
Lejos de hacer la paz, la gira de Bush fue para mantener a los palestinos bajo la bota de Israel y consolidar apoyo para EEUU contra Ir&aacute;n.<p>

