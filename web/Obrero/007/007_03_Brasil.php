<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ELECCIONES EN BRASIL</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Triunfa Lula &#191;Ahora qu&eacute;?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Por Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | 22 de noviembre de 2002 | P&aacute;gina 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">El candidato de la izquierda, Luiz In&aacute;cio "Lula" da Silva, del Partido de los Trabajadores (PT), gan&oacute; contundentemente las elecciones presidenciales en Brasil a finales de octubre. "Esto es...un cambio hist&oacute;rico en la trayectoria de este pa&iacute;s, que muestra cu&aacute;nto desea el cambio," dijo Candido Mejora, reconocido analista pol&iacute;tico brasile&ntilde;o. "Lula ha ganado en cada regi&oacute;n del pa&iacute;s y en cada estrato de la sociedad."</P>
<P>El antiguo trabajador de f&aacute;brica y l&iacute;der sindical gan&oacute; el 61 por ciento del voto--la victoria m&aacute;s abrumadora en la historia brasile&ntilde;a. Su triunfo instala un partido izquierdista en el gobierno de Brasil por primera vez en 40 a&ntilde;os. Ahora Lula encarar&aacute; el inmenso desaf&iacute;o de cumplir con las grandes expectativas de quienes lo eligieron presidente. </P>
<P>Por un lado, los trabajadores, los campesinos sin tierras y los pobres que votaron por Lula esperan ver cumplidas las promesas de aumentar sueldos, crear trabajos y financiar programas que resuelvan la crisis de hambre en el pa&iacute;s. Otros votaron por Lula por su postura cr&iacute;tica al ALCA (&Aacute;rea de Libre Comercio de las Am&eacute;ricas) y a la creciente subordinaci&oacute;n econ&oacute;mica y pol&iacute;tica de Brasil a los EE.UU.</P>
<P>Pero por otro lado, un sector significativo de la &eacute;lite brasile&ntilde;a apoy&oacute; a Lula tambi&eacute;n--y espera algo muy diferente. Durante a&ntilde;os, la pol&iacute;tica neoliberal del Presidente Fernando Henrique Cardoso aument&oacute; la pobreza y estanc&oacute; la econom&iacute;a del pa&iacute;s. Ahora estos patrones quieren un presidente que pueda mantener la paz social mientras que aplique medidas de austeridad. </P>
<P>Mientras tanto, los inversionistas extranjeros que sacaron miles de millones de d&oacute;lares de Brasil en meses anteriores--desvalorizando la moneda del pa&iacute;s un 38 por ciento el a&ntilde;o pasado--ahora escudri&ntilde;an cualquier se&ntilde;al de que Lula quiz&aacute;s considere dejar de pagar la deuda masiva que Brasil tiene. Desde hace meses Lula ha fervientemente asegurado a los banqueros que &eacute;l s&iacute; est&aacute; comprometido a pagar la deuda--y que est&aacute; receptivo a un tipo de ALCA "m&aacute;s amable, m&aacute;s apacible". Lula escogi&oacute; a Jos&eacute; Alencar como su compa&ntilde;ero de lista, un industrialista que no soporta las uniones e hizo una fortuna suministrando a compa&ntilde;&iacute;as como Walmart con productos textiles baratos mientras pagaba a sus trabajadores un sueldo miserable. </P>
<P>A pesar de estas concesiones a la clase empresarial, los mercados monetarios siguen azotando la econom&iacute;a del pa&iacute;s. "Una vez que acaben las elecciones, al Sr. da Silva no le quedar&aacute; tiempo que perder...s&oacute;lo al reducir algunas de sus promesas--de aumentar el salario m&iacute;nimo, de gastar m&aacute;s en programas sociales--&eacute;l quiz&aacute;s sea capaz de aplacar los temores en el mercado. Sin embargo, son estas mismas pol&iacute;ticas, por lo menos en parte, las que produjeron el viraje hacia &eacute;l. Si fracasa al cumplir con estas promesas, le podr&iacute;a perjudicar al buscar un segundo mandato", se jact&oacute; la revista <I>The Economist.</I></P>
<P>Desde el momento en que Lula asuma el poder en enero, se asomar&aacute; el potencial de las luchas desde abajo en varios sectores contra los efectos de la crisis brasile&ntilde;a.</P>

