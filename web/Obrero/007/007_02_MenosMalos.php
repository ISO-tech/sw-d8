<font face="Times New Roman, Times, serif" size="4">Dem&oacute;cratas tienen toda la culpa de su derrota electoral</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>El mal de los menos malos</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">22 de noviembre de 2002 | P&aacute;gina 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3">Los republicanos estaban insaciablemente engre&iacute;dos tras su reciente victoria electoral. Ganaron suficientes cargos en el Senado como para superar la &iacute;nfima mayor&iacute;a de un voto de los Dem&oacute;cratas, mantuvieron su escasa mayor&iacute;a en la C&aacute;mara de Representantes y ganaron varias batallas grandes en las contiendas para gobernador.</P>
<P>Ahora los oficiales del partido y los comentaristas hablan acerca de los resultados como un endoso a la presidencia de Bush--y como un mandato para empujar su agenda derechista y su guerra contra Irak. &#191;Pero qu&eacute; fue lo que pas&oacute;?</P>
<P>Si cada sondeo de opini&oacute;n p&uacute;blica previo a la elecci&oacute;n se&ntilde;alaba que los "temas dem&oacute;cratas"--la econom&iacute;a estancada , el Seguro Social, los servicios de salud--estaban al tope de las preocupaciones de los electores. Y como quiera los dem&oacute;cratas perdieron. </P>
<P>Una explicaci&oacute;n t&iacute;pica de los dem&oacute;cratas es que los republicanos triunfaron al desviar la atenci&oacute;n lejos de la mediocre econom&iacute;a, haciendo la guerra contra Irak el asunto pol&iacute;tico central de los &uacute;ltimos meses.</P>
<P>As&iacute; fue como los l&iacute;deres dem&oacute;cratas justificaron su voto a favor de la exigencia de la Casa Blanca de una resoluci&oacute;n congresional autorizando la acci&oacute;n militar--supuestamente para sacar el asunto "fuera del caldero". Pero tan s&oacute;lo un dem&oacute;crata de los que desafiaron a su liderato y votaron contra la campa&ntilde;a de guerra perdi&oacute; en la elecci&oacute;n. Mientras tanto, respecto al asunto que ellos supuestamente quer&iacute;an discutir--acerca de la econom&iacute;a--los dem&oacute;cratas no ten&iacute;an nada que ofrecer.</P>
<P>Incre&iacute;blemente, esto le dio la oportunidad a los republicanos de reclamar el tener un "plan" ayudar a la gente com&uacute;n mientras que los dem&oacute;cratas s&oacute;lo hicieron cr&iacute;ticas. Un verdadero desaf&iacute;o al programa econ&oacute;mico de Bush habr&iacute;a significado atacar el recorte de 1.35 trillones de d&oacute;lares en impuestos con que la Casa Blanca obsequi&oacute; a los estadounidenses m&aacute;s ricos el a&ntilde;o pasado. </P>
<P>Problema: el recorte de impuestos de Bush fue aprobado por el Congreso siendo los dem&oacute;cratas quienes proporcionaron el margen de victoria. Como resultado, ni una sola figura dem&oacute;crata con credibilidad tuvo las agallas para exigir que los recortes de impuesto fueran revocados. Y entonces los dem&oacute;cratas descubrieron la noche de las elecciones que uno no puede derrotar a nadie si no ofrece nada.</P>
<P>La elecci&oacute;n del 2002 le dar&aacute; a los republicanos una mayor&iacute;a en ambas c&aacute;maras del Congreso, sum&aacute;ndose a su control de la Casa Blanca y la corte federal. Esto es una situaci&oacute;n inquietante. Y los republicanos pueden tratar de salirse con la suya en proyectos favoritos que los dem&oacute;cratas hab&iacute;an bloqueado--como una prohibici&oacute;n de los abortos durante el &uacute;ltimo trimestre del embarazo que fan&aacute;ticos anti-aborto han mal denominado "abortos de nacimiento parcial". </P>
<P>La clave para prevenir que la pandilla de Bush siga adelante con su agenda de guerra, ataques contra los trabajadores y d&aacute;divas a los ricos es la misma que era antes de las elecciones del 5 de noviembre--movilizar una oposici&oacute;n a nivel de base para derrotarlos. Si la pandilla de Bush piensa que ellos tienen un mandato, est&aacute;n equivocados. </P>
<P>Los republicanos s&oacute;lo lograron sus ganancias camuflajeando la mayor&iacute;a de sus posturas pol&iacute;ticas m&aacute;s detestadas y los dem&oacute;cratas dejaron que ellos se salieran con la suya. Cu&aacute;ndo la careta se les caiga, el programa de la pandilla de Bush ser&aacute; expuesto como el ataque vicioso derechista que verdaderamente es.</P>
<P>Y eso s&oacute;lo alimentar&aacute; la amargura de decenas de millones de trabajadores. Necesitamos organizar esa c&oacute;lera en una oposici&oacute;n activa a los ataques derechistas de la pandilla de Bush.</P>

