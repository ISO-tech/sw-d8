<P><font face="Times New Roman, Times, serif" size="4">"Benefactor" Latino es un patr&oacute;n avaro</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Huelga en Azteca Foods</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Por H&eacute;ctor Reyes</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | 22 de noviembre de 2002 | P&aacute;gina 4</font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--Luego de nueve semanas en huelga, los trabajadores de Azteca Foods todav&iacute;a est&aacute;n en pie de lucha resueltos a darse a respetar con la compa&ntilde;&iacute;a y a ganarse un contrato decente.</P>
<P>La f&aacute;brica de Azteca Foods produce suficientes tortillas como para venderlas por todo el pa&iacute;s y generar m&aacute;s de $30 millones al a&ntilde;o en ingresos. Sin embargo m&aacute;s de un tercio de su fuerza laboral ha trabajado para Azteca por m&aacute;s de 20 a&ntilde;os y gana s&oacute;lo $9 por hora.</P>
<P>Por d&eacute;cadas los trabajadores fueron representados por un sindicato corrupto infiltrado por el crimen organizado que nunca trat&oacute; de hacer respetar un verdadero contrato. Cuando los trabajadores se hartaron de este arreglo injusto, y pidieron que los representara al Local 1159 del Sindicato de Trabajadores de la Industria El&eacute;ctrica, de Radio y Maquinarias de Am&eacute;rica (UE, por sus siglas en ingl&eacute;s), el due&ntilde;o de Azteca, Arthur Vel&aacute;squez se rehus&oacute; reconocer la uni&oacute;n.</P>
<P>"El Sr. Vel&aacute;squez no est&aacute; acostumbrado a respetar una uni&oacute;n porque &eacute;l est&aacute; acostumbrado a tratar con puras uniones corruptas", dijo la huelguista Angelina D&iacute;az a <I>Obrero Socialista</I>. "Entonces ahora que entr&oacute; una verdadera uni&oacute;n con nosotros, por eso &eacute;l no la acepta".</P>
<P>Mientras que Vel&aacute;squez espera que los trabajadores se traguen un aumento de 700 por ciento en la prima del seguro m&eacute;dico para cubrir a sus familias, &eacute;l no ofrece ning&uacute;n aumento de salario en el primer a&ntilde;o del contrato y un aumento miserable de cinco centavos por hora para los dos a&ntilde;os siguientes. Esto equivale a una reducci&oacute;n de su salario.</P>
<P>Todos los 63 huelguistas son inmigrantes latinos y latinas. Y siempre est&aacute;n listos para enfatizar la contradicci&oacute;n que existe entre la imagen de Vel&aacute;squez en la comunidad latina de Chicago como benefactor de organizaciones culturales y comunitarias, y la manera tir&aacute;nica y avara con que &eacute;l trata a sus empleados.</P>
<P>"El Sr. Vel&aacute;squez en la comunidad tiene una cara muy bonita, pero ante sus trabajadores, mire c&oacute;mo nos tiene aqu&iacute;--pidiendo hasta comida", dijo la huelguista Josefina Bonilla. "&#191;No le da verg&uuml;enza a este se&ntilde;or?. Que dondequiera ofrece ayuda, y los que lo hacen rico estamos pidiendo ayuda".</P>
<P>Entre muchas barbaridades, Vel&aacute;squez ha tratado de intimidar a sus trabajadores, amenaz&aacute;ndolos con despedirlos por participar en l&iacute;neas de piquetes. Y para colmo, este gran "benefactor" le ha quitado las becas a los hijos de los empleados en huelga, y se ha rehusado a garantizar que las negociaciones se conduzcan en forma biling&uuml;e--produciendo todas sus propuestas escritas estrictamente en ingl&eacute;s. Pero como dice el viejo dicho popular, "Los ricos con los ricos y los pobres con los pobres".</P>
<P>Y as&iacute; mientras que Vel&aacute;squez recibe el apoyo de la C&aacute;mara de Comercio del barrio mexicano La Villita, los trabajadores han recibido apoyo de la comunidad latina, de organizaciones religiosas y de otras uniones. Los trabajadores de la compa&ntilde;&iacute;a de entregas UPS se han rehusado a hacer entregas en Azteca, como tambi&eacute;n muchos conductores de camiones.</P>
<P>La uni&oacute;n ha comenzado una campa&ntilde;a de boicot de las tortillas de Azteca Foods, y para eso est&aacute; apelando a otros trabajadores a que no consuman este producto. Tambi&eacute;n est&aacute; acudiendo a los supermercados para pedirles que se rehusen a aceptar m&aacute;s de estas tortillas hasta que Azteca firme un convenio justo con sus trabajadores.</P>

<P><I>Para apoyar a los trabajadores de Azteca o ayudar con la campa&ntilde;a de boicot, comun&iacute;cate con el Local 1159 de UE al 312-829-8300.</I> </P>

