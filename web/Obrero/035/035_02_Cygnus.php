<font face="Times New Roman, Times, serif" size="5"><b>Una victoria con lecciones para el movimiento laboral</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Por Lee Sustar y Orlando Sep&uacute;lveda</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | octubre-noviembre de 2007 | p&aacute;gina 2</font><p>

<font face="Times New Roman, Times, serif" size="3">UNA NOTABLE victoria, despu&eacute;s de dos semanas de huelga bajo el caliente sol de agosto, y sin sindicato, consiguieron m&aacute;s de 100 obreros inmigrantes contra la f&aacute;brica de jab&oacute;n Cygnus Corp., al sur de Chicago.<p>
Los trabajadores coronaron su victoria votando a mano alzada la oferta de la compa&ntilde;&iacute;a y de la agencia de trabajo temporal, Total Staffing Inc., de aceptarlos de vuelta sin represalias, y con las amenaza de cesaci&oacute;n en su contra cancelada.<p>
Estos trabajadores inmigrantes mexicanos prevalecieron sobre la gerencia de Cygnus, apoyada por su casa matriz, Marietta Corp., fabricante de jabones y detergentes con marcas de tienda para minoristas tales como Wal-Mart, Target y Walgreens, y por Ares Management, una firma accionista privada que controla Marietta y que est&aacute; valorada en $16 mil millones.<p>
Cygnus quiso utilizar las cartas No-Match --una notificaci&oacute;n del gobierno indicando que los n&uacute;meros de Seguro Social en archivo no concuerdan con &eacute;sos dados por los empleados --para amenazar con despido a los pocos trabajadores permanentes, luego que estos pidieron colectivamente un aumento salarial. Por su parte, a los trabajadores temporales les fue demandado cambiar de agencia, donde ellos tendr&iacute;an que re-verificar su estatus.<p>
En respuesta, Cygnus fue golpeada por una huelga casi total, con animados piquetes, apoyo solidario y la promesa de un sindicato para organizarlos. Una operaci&oacute;n rompe huelga se desinfl&oacute;, con m&aacute;s y m&aacute;s camiones saliendo de la planta sin carga.<p>
A la segunda semana, las gerencias de Cygnus y de Total Staffing fueron obligadas por los obreros a coordinar sus ofertas, despu&eacute;s que estos demandaron una soluci&oacute;n para todos los empleados, permanentes y temporeros, venciendo as&iacute; por sobre los esfuerzos de las compa&ntilde;&iacute;as a dividirlos. En una reuni&oacute;n en un parque p&uacute;blico, Edith, una de las empleadas permanentes, y l&iacute;der de la huelga, dijo: "No hay permanente o temporal --somos todos trabajadores."<p>
En el contexto de un movimiento laboral que no puede salir del estanco, y que no ha encontrado la energ&iacute;a o recursos para seguir organizando y crecer, la huelga de Cygnus expone el poder que tienen trabajadores que est&aacute;n listos y dispuestos a organizarse.<p>
La primera acci&oacute;n colectiva de los trabajadores de Cygnus fue negociar su salida a las marchas de marzo y mayo del 2006, igual que fue hecho en incontable plazas laborales sin sindicato alrededor de EEUU. Ellos fueron exitosos porque los trabajadores indocumentados estaban dispuestos a arriesgar sus trabajos &#8211; o peor &#8211; por participar.<p>
Adem&aacute;s, activistas pro-inmigrante locales estuvieron listos para responder a las necesidades de los trabajadores con consejo legal y pol&iacute;tico, y para organizar solidaridad con los trabajadores. Redes similares existen en ciudades donde el movimiento laboral y el pro-inmigrante son m&aacute;s fuertes, como Nueva York, Los &Aacute;ngeles, y Seattle.<p>
Tambi&eacute;n, los obreros de Cygnus demostraron que la organizaci&oacute;n de un sindicato no necesita ser la pesadilla burocr&aacute;tica, como lo son la mayor&iacute;a de esfuerzos organizativos sindicales por la resistencia del patr&oacute;n. De hecho, ellos mostraron qu&eacute; la victoria es posible cuando los trabajadores toman la iniciativa de la huelga.<p>
"El movimiento laboral tiene mucho que aprender de los trabajadores [de Cygnus], porque no podr&aacute; avanzar se deja a los trabajadores inmigrantes de lado," dijo Mart&iacute;n Unzueta de Chicago Workers Collaborative, que sirvi&oacute; como consejero a los huelguistas, "los trabajadores inmigrantes est&aacute;n listos para organizar."<p>

