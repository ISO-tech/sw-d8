<P><font face="Times New Roman, Times, serif" size="5"><b>Los horrores que no nos mostrar&aacute;n</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">18 de abril de 2003 | P&aacute;gina 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3">EL PADRE de Bakhat Hassan se puso su mejor traje para ir hacia el &aacute;rea controlada por el ej&eacute;rcito de EE.UU. en las inmediaciones del pueblo iraqu&iacute; de Karbala. "Para verse americano", dijo Bakhat. </P>
<P>Fue as&iacute; vestido que muri&oacute;. Los padres de Bakhat, sus dos hijas, un hijo, dos hermanos, sus esposas y dos sobrinas fueron asesinados cuando soldados de EE.UU. abrieron fuego contra su camioneta cuando se dirig&iacute;a hacia un punto de control. "Mis ni&ntilde;as--vi sus cabezas desprenderse de sus cuerpos", dijo estupefacta Lamea, la esposa de Hassan, a un periodista australiano desde su cama de hospital. La pobre mujer, quien est&aacute; embarazada, agreg&oacute;, "Ser&iacute;a mejor no tener al beb&eacute;. Nuestras vidas est&aacute;n acabadas". </P>
<P>La familia de 17 personas se api&ntilde;&oacute; en una camioneta para viajar a Karbala, pensando que un folleto tirado desde un helic&oacute;ptero estadounidense dec&iacute;a que ellos "estar&iacute;an seguros" all&iacute;. Seg&uacute;n el recuento de Bakhat en el <I>Sydney Morning Herald </I>de Australia, cuando su camioneta se acercaba a un punto de control al sur de Karbala, su familia salud&oacute; a los soldados americanos y los soldados le abrieron fuego.</P>
<P>"Ten&iacute;amos esperanza" dijo Bakhat. "Pero entonces ustedes los americanos vinieron a traernos democracia, y nuestra esperanza se extingui&oacute;". </P>
<P>Esta es una de muchas historias horribles ahora en Irak. Y nos est&aacute;n siendo ocultadas. Ocultadas por los pol&iacute;ticos y por los jefes del Pent&aacute;gono que nos venden sus mentiras acerca de una guerra para "liberar" al pueblo iraqu&iacute;. Ocultadas por los medios noticiosos corporativos que bien pudieran ser administrados por el estado, porque con tanto gusto repiten la propaganda del ej&eacute;rcito.</P>
<P>Por lo menos 61 han muerto en la regi&oacute;n de Hilla, 50 millas al sur de Bagdad--y las v&iacute;ctimas tienen numerosas "heridas viciosas y profundas", caracter&iacute;sticas de bombas de dispersi&oacute;n, seg&uacute;n el veterano periodista Robert Fisk del per&oacute;dico brit&aacute;nico <I>The Independent. </I>Pero el Pent&aacute;gono insiste que "no hay indicaciones" de que bombas de dispersi&oacute;n fueron usadas en la cercan&iacute;a de Hilla.</P>
<P>Por lo menos 62 fueron matados en el bombardeo del mercado Shu'ale en Baghdad, con un pedazo de la cubierta del proyectil, hecho en EE.UU. por el prominente contratista militar Raytheon, encontrado en el macabro escenario. Pero los oficiales estadounidenses y brit&aacute;nicos reclaman que los iraqu&iacute;es bombardearon el mercado ellos mismos y que plantaron la cubierta del proyectil. </P>
<P>&#191;No hay nada que estos monstruos no puedan decir para justificar su guerra?  Nadie sabe cu&aacute;ntos miles de iraqu&iacute;es han muerto ya, pero "el n&uacute;mero de v&iacute;ctimas en Bagdad es tan alto que los hospitales han dejado de contar el n&uacute;mero de personas atendidas", seg&uacute;n un informe de Reuters que cita a la Cruz Roja.  Y literalmente millones de iraqu&iacute;es estaban bajo riesgo de enfermedad e inanici&oacute;n a causa del estado de sitio anglo-americano de Bagdad y Basra.</P>
<P>Los medios estadounidenses enfatizaron el rol de los aliados en la distribuci&oacute;n de la "ayuda humanitaria" a los iraqis. Pero esa ayuda es insuficiente comparada con la destrucci&oacute;n que las fuerzas invasoras han causado en Irak.</P>
<P>Que luego se han tornado en escenarios de saqueos masivos y motines una vez estos ej&eacute;rcitos lograron tomar grandes porciones de ambas ciudades. Gente de todo el mundo y tambi&eacute;n muchos en EE.UU. han repudiado este horror. Hemos tenido la raz&oacute;n al desenmascarar esta carnicer&iacute;a--llevada a cabo en nombre de la "paz" y "la liberaci&oacute;n". </P>
<P>Y tenemos que seguir confrontando a los criminales de guerra de Washington con nuestro mensaje: &#161;No a la guerra por petr&oacute;leo e imperio!</P>

