<P><font face="Times New Roman, Times, serif" size="5"><b>Ciudadan&iacute;a despu&eacute;s de la muerte</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>por Bridget Broderick</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | 18 de abril de 2003 | P&aacute;gina 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">UNA DE las v&iacute;ctimas del los ataques del 11 de septiembre fue la aminist&iacute;a para los inmigrantes en EE.UU. Pero con los ataques de Bush contra Irak, los inmigrantes tienen otras oportunidades de obtener la ciudadan&iacute;a: morir en combate.</P>
<P>Siete soldados inmigrantes en el Golfo recibieron este honor recientemente. Qu&eacute; pena que no lo podr&aacute;n disfrutar--recibieron su ciudadan&iacute;a p&oacute;stuma en la guerra contra Irak.</P>
<P>Han publicado los nombres de tres: Diego Rinc&oacute;n, colombiano de 19 a&ntilde;os; Jos&eacute; Angel Garibay, mexicano de 21 a&ntilde;os; y Jos&eacute; Guti&eacute;rrez, guatemalteco de 27 a&ntilde;os. Hay unos 37,000 hombres y mujeres--casi 3 por ciento de las fuerzas militares activas--que no son ciudadanos estadounidenses. De &eacute;stos, 9,871 son latinoamericanos que carecen de ciudadan&iacute;a, y de ellos 3,936 son mexicanos y 221 cubanos. </P>
<P>Muchos de ellos se inscribieron esperando obtener m&aacute;s r&aacute;pido la ciudadan&iacute;a. Pero seg&uacute;n un decreto firmado por Bush el julio pasado, los miles de soldados latinos que luchan por el go-bierno de EE.UU. en Irak s&oacute;lo obtendr&aacute;n su pasaporte, un paso parcial hacia el 'sue&ntilde;o americano' de la ciudadan&iacute;a. Mientras que pueden solicitar la naturalizaci&oacute;n antes de los cinco a&ntilde;os (l&iacute;mite m&iacute;nimo para otros inmigrantes), el proceso de solicitud se puede retrasar varios a&ntilde;os.</P>
<P>La legislaci&oacute;n propuesta por los senadores Zell Miller and Saxby Chambliss le otorgar&aacute; la ciudadan&iacute;a inmediata a unos soldados combatientes en el Golfo. Pero s&oacute;lo aplica a los que mueran en combate. La iron&iacute;a de otorgarles ciudadan&iacute;a a los muertos no pas&oacute; desapercibida para algunas figuras prominentes. </P>
<P>El arzobispo de Los Angeles, la mayor arquidi&oacute;cesis cat&oacute;lica del pa&iacute;s, el Cardenal Roger Mahoney, solicit&oacute; al presidente que le concediera la ciudadan&iacute;a a todo soldado inmigrante que cumple servicio en el Golfo P&eacute;rsico. "Hay algo completamente err&oacute;neo en nuestra pol&iacute;tica de inmigraci&oacute;n cuando uno tiene que dejar la vida en el campo de batalla para ser ciudadano", escribi&oacute; el cardenal al presidente Bush. </P>
<P>La hermana de Guti&eacute;rrez, Engracia, habl&oacute; desde Guatemala sobre la guerra que mat&oacute; a su hermano: "No se puede esperar nada bueno de esto. Es malo. Yo soy amante de la paz y para m&iacute;, la guerra s&oacute;lo deja el dolor en el coraz&oacute;n".</P>

