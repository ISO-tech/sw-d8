<P><font face="Times New Roman, Times, serif" size="5"><b>Apoya a los obreros de Cintas</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>por Orlando Sep&uacute;lveda</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | 18 de abril de 2003 | P&aacute;gina 4</font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--UNITE est&aacute; acelerando su plan de organizar a los trabajadores en Cintas, la m&aacute;s grande lavander&iacute;a industrial y proveedor de uniformes rentados en Norte Am&eacute;rica.</P>
<P>La compa&ntilde;&iacute;a report&oacute; ganancias el a&ntilde;o pasado de $234 millones, cumpliendo as&iacute; 33 a&ntilde;os de crecimiento. Pero este &eacute;xito ha sido alcanzado disminuyendo est&aacute;ndares de seguridad, quebrando la ley, acosando trabajadores y destruyendo sus sindicatos, todo por lo cual el CEO de Cintas Richard Farmer se ha hecho malamente famoso.</P>
<P>Por esto, intentando representar a los trabajadores en Cintas, UNITE ha lanzado la campa&ntilde;a "Uniforma la Justicia" interponiendo 82 cargos contra la compa&ntilde;&iacute;a por violaci&oacute;n de las leyes federales del trabajo. De acuerdo al reporte preparado por UNITE, "Cintas: La Mugrosa Verdad detr&aacute;s de los Uniformes", la compa&ntilde;&iacute;a ha desplegado "un regimiento de personal de seguridad...para registrar actividad sindical e intimidar a los trabajadores," y usa "reuniones forzadas e interrogaciones personales" para ejercer presi&oacute;n sobre sus empleados.</P>
<P>Este tipo de actividad anti-sindical es muy conocida por Miguel C., quien trabaj&oacute; para la compa&ntilde;&iacute;a hasta que fue despedido en febrero. "Yo hab&iacute;a hablado con el supervisor para irme m&aacute;s temprano, y el me dijo que estaba bien. Pero al pr&oacute;ximo d&iacute;a &eacute;l neg&oacute; que nosotros hab&iacute;amos hablado, y luego me despidieron. La verdad es que me despidieron porque yo me un&iacute; al sindicato," &eacute;l le dijo a Obrero Socialista en el local de UNITE, donde estaba participando en la realizaci&oacute;n de un video para exponer estas pr&aacute;cticas.</P>
<P>Puedes encontrar m&aacute;s informaci&oacute;n en la internet en www.uniformjustice.org, o puedes llamar al 1-800-872-8646.</P>

