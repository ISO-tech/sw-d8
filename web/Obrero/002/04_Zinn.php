<P><font face="Times New Roman, Times, serif" size="4">HOWARD ZINN:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Ellos perpet&uacute;an un ciclo del terrorismo"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">14 de Septiembre de 2001 | P&aacute;gina 4</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>Howard Zinn, el autor de "A Peoples's History of the United States" y un veterano activista en contra de la guerra, explica las consecuencias pol&iacute;ticas de los ataques.</b></P>
<P>ES UNA situaci&oacute;n delicada, en la cual tenemos que hacer claro que entendemos el dolor y la angustia que las personas sienten. Entendemos incluso el grito intuitivo que pide castigo y venganza. Pero no debemos dejar que esa reacci&oacute;n emocional inmediata gobierne lo que hacemos, lo que debe estar basado en una reflecci&oacute;n de c&oacute;mo podemos prevenir m&aacute;s violencia--tanto de los terroristas como de los gobiernos.</P>
<P>El gobierno de los Estados Unidos va a responder--y los medios noticiosos concurrir&aacute;n como corderitos con el llamado de acci&oacute;n militar, de aumentos en el presupuesto militar, que es exactamente lo que hace al terrorismo inevitable.</P>
<P>La ayuda militar y el apoyo de EE.UU. a Israel crea furia y resentimiento en el mundo &aacute;rabe,  haciendo que una peque&ntilde;a porci&oacute;n de los furiosos y resentidos planee ataques terroristas en represalia. La respuesta militar al terrorismo solo perpet&uacute;a el ciclo de terrorismo y contra-terrorismo.</P>
<P>El continuo gasto de m&aacute;s de $300 billones para la armada no tiene absolutamente ning&uacute;n efecto. Si queremos seguridad verdadera, tendremos que cambiar nuestra postura en el mundo--dejar de ser una fuerza de intervenci&oacute;n militar y dejar de dominar las econom&iacute;as de otros pa&iacute;ses.</P>
<P>Un reporte del Junta Cient&iacute;fica de Defensa a el gobierno de los EE.UU. en 1997 demostr&oacute; "una fuerte correlaci&oacute;n entre el envolvimiento de los EE.UU. en las situaciones mundiales y un incremento en el terrorismo". Tenemos inmensas bases militares en 19 pa&iacute;ses, y esto inevitablemente trae problemas.</P>
<P>Lo que Bush est&aacute; proponiendo ahora es justamente lo que otros presidentes han propuesto antes--Reagan, Clinton, ambos partidos--desde la Segunda Guerra Mundial: perseguir el dominio de grandes &aacute;reas del mundo. El horror que experimentamos hoy es algo que personas de otras partes del mundo--Asia suroriental, Irak, Yugoslavia--han experimentado como resultado de nuestros bombardeos.</P>
<P>Esto deber&iacute;a hacernos recapacitar sobre cualquier deseo de continuar con soluciones militares. </P>

