<p><a name="FrontAndBackPages"></a><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>PRIMERA PLANA Y PAGINA TRASERA</b></font></p>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="01_Tragedia.shtml">Que no conviertan la tragedia en guerra</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">Mientras que la gente alrededor del mundo lidiaban con la enormidad de las p&eacute;rdidas humanas en los ataques sobre Nueva York y Washington, D.C., George W. Bush y su gobierno redoblaban los tambores de guerra en pos de a&uacute;n m&aacute;s muerte y destrucci&oacute;n.</P>

<P><font face="Times New Roman, Times, serif" size="3"><b>El racismo enfermizo de la prensa incita a ataques contra &aacute;rabes</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><a href="04_NoAlRacismo.shtml">&#161;No al racismo y los chivos expiatorios!</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">Los medios noticiosos estaban repletos de exabruptos anti-&aacute;rabes y anti-isl&aacute;micos luego de los ataques de Nueva York y Washington, D.C. Y escondi&eacute;ndose detr&aacute;s de la simpat&iacute;a por las v&iacute;ctimas de los ataques, los racistas a trav&eacute;s de los EE.UU. aprovecharon el tono. </P>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="01_Vigilias.shtml">Miles asisten a vigilias clamando por paz</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">Mientras los pol&iacute;ticos claman por la guerra, miles de personas asistieron a vigilias a trav&eacute;s del pa&iacute;s en duelo por las v&iacute;ctimas de los ataques en Nueva York y Washington--y para demostrar su oposici&oacute;n a m&aacute;s violencia.</P>

