<p><a name="Analysis"></a><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>ANALYSIS Y TRASFONDO</b></font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>El gobierno de los EE.UU.: </b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b><a href="02_Violencia.shtml">"Los despachadores m&aacute;s grandes de violencia en el mundo"</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">El gobierno de los EE.UU. reaccion&oacute; a los ataques en Nueva York y Washington, D. C., de la &uacute;nica forma que siempre lo ha hecho--arm&aacute;ndose para imponer despiadadamente sus intereses militares, pol&iacute;ticos y econ&oacute;micos alrededor del mundo. Es esta historia lo que explica por qu&eacute; millones de personas a trav&eacute;s del globo desprecian al gobierno de los EE.UU.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="03_LaVerdad.shtml">La verdad es la primera baja de la guerra</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">El gobierno de los EE.UU. tiene una larga historia de manipulaci&oacute;n de la verdad--hasta de mentir descaradamente--para obtener la aprobaci&oacute;n p&uacute;blica para sus aventuras militares.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="04_PearlHarbor.shtml">La verdad genuina acerca de Pearl Harbor</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">"Esto es Pearl Harbor para la edad del terrorismo", escribi&oacute; un columnista de Chicago acerca de los ataques de avi&oacute;n suicidas. Pero la historia detr&aacute;s del asalto a&eacute;reo japon&eacute;s de diciembre de 1941 revela m&aacute;s de lo que los medios quisieran que supi&eacute;ramos. </P>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="03_Palestina.shtml">Como los EE.UU. apoya la violencia contra Palestina</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">Osama bin Laden es el primero en la lista de los sospechosos sin tomar en cuenta que &eacute;l neg&oacute; cualquier conecci&oacute;n con los ataques. Pero los pol&iacute;ticos r&aacute;pidamente culparon a los palestinos tambi&eacute;n--de nuevo, a pesar del hecho de que el l&iacute;der de la Autoridad Palestina, Yasser Arafat y todas las organizaciones palestinas condenaron el ataque.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b><a href="02_OsamaBinLaden.shtml">Como la CIA entren&oacute; a Osama bin Laden</a></b></font><BR>
<font face="Times New Roman, Times, serif" size="3">Bin Laden ha sido por a&ntilde;os el cuco favorito del establecimiento pol&iacute;tico de Washington. Pero si bin Laden est&aacute; envuelto en terrorismo, &eacute;l fue educado por los mejores--el gobierno de los Estados Unidos.</P>

