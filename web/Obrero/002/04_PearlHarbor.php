<P><font face="Times New Roman, Times, serif" size="5"><b>La verdad genuina acerca de Pearl Harbor</b></font> </P>
<p><font face="Arial, Helvetica, sans-serif" size="2">14 de Septiembre de 2001 | P&aacute;gina 4</font></p>

<P><font face="Times New Roman, Times, serif" size="3">"ESTO ES Pearl Harbor para la edad del terrorismo", escribi&oacute; un columnista de Chicago acerca de los ataques de avi&oacute;n suicidas. Pero la historia detr&aacute;s del asalto a&eacute;reo japon&eacute;s de diciembre de 1941 revela m&aacute;s de lo que los medios quisieran que supi&eacute;ramos. </P>
<P>Los oficiales de los EE.UU. hab&iacute;an interceptado los mensajes japoneses y sab&iacute;an del ataque inminente antes de que sucediera--pero no hicieron nada porque ellos quer&iacute;an que Jap&oacute;n hiciera el primer disparo. El entonces Secretario de Estado Henry Stimson dijo al Congreso despu&eacute;s de la guerra que el gobierno de los EE.UU. quiso manipular a Jap&oacute;n para que "hiciera el primer disparo...para obtener el completo apoyo del pueblo estadounidense" para la entrada de los EE.UU. en la guerra. </P>
<P>Despu&eacute;s del ataque--en que m&aacute;s de 2,000 soldados de los EE.UU. perdieron su vida--el gobierno de los EE.UU. y los medios noticiosos iniciaron una campa&ntilde;a hist&eacute;rica contra los japon&eacute;s-americanos. El presidente Roosevelt firm&oacute; la Orden Ejecutiva 9066, que intern&oacute; a m&aacute;s de 120,000 japon&eacute;s-americanos en campos de concentraci&oacute;n. </P>
<P>El ataque del Pearl Harbor fue usado tambi&eacute;n para justificar el tirar bombas at&oacute;micas en Hiroshima y Nagasaki en agosto de 1945--cost&aacute;ndole la vida a m&aacute;s de 200,000 personas. </P>
<P>El presidente Truman justific&oacute; el bombardeo at&oacute;mico sobre la premisa de que salv&oacute; vidas al propiciar un fin m&aacute;s r&aacute;pido de la guerra. Pero Jap&oacute;n estaba ya listo para rendirse antes de que las bombas fueran lanzadas. Como el General Mayor Curtis LeMay observ&oacute; en septiembre de 1945, "La bomba at&oacute;mica no tuvo nada que ver con el fin de la guerra". </P>
<P>La raz&oacute;n verdadera por Hiroshima y Nagasaki era demostrar a Rusia y al resto del mundo el poder impresionante de la nueva arma de terror en masa de los EE.UU. </P>
<P>Los paralelos entre el Pearl Harbor y el ataque terrorista reciente nos conducen a hacer la misma pregunta propuesta por Alexander Cockburn y Jeffrey St. Clair. "&#191;Se supone que nos creamos ", ellos escribieron, "que el presupuesto anual de 30 billones de d&oacute;lares en inteligencia, la inmensa capacidad de espionaje electr&oacute;nico, y miles de agentes alrededor del mundo, produjeron nada en la forma de una advertencia?" </P></FONT></BODY>

