<P><font face="Times New Roman, Times, serif" size="4">NORMAN SOLOMON:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Un periodismo que inventa historias"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">14 de Septiembre de 2001 | P&aacute;gina 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>Norman Solomon, un columnista sindicado que escribe sobre pol&iacute;tica y los medios noticiosos, habl&oacute; con <I>Obrero Socialista </I>acerca de la respuesta de la prensa com&uacute;n a los ataques a&eacute;reos.</b></P>
<p>El adagio de George Orwell es escalofriantemente apropiado. El escribi&oacute;, "Aquellos que controlan el pasado controlan el futuro. Aquellos que controlan el presente, controlan el pasado".</P>
<P>Hablar de Osama bin Laden sin mencionar sus relaciones con la CIA durante la d&eacute;cada de los ochenta en Afganist&aacute;n, es tirar la historia a un safac&oacute;n. Y observando hora tras hora la red televisiva durante las &uacute;ltimas 24 horas, yo no pude encontrar la m&aacute;s m&iacute;nima referencia a ello.</P>
<P>Nosotros tenemos unos medios noticiosos en los Estados Unidos que simplemente reinventan la historia a su conveniencia. </P>
<P>Esto esta impulsado mayormente por la gran proximidad entre la prensa y el estado--y el hecho de que muchas de las presunciones que tienen peso en la oficina presidencial, el Pent&aacute;gono o el Departamento de Estado, son las mismas presunciones que tienen peso en los estudios de los principales medios de prensa.</P>
<P>&#9;A solo horas del ataque al <I>World Trade Center</I> y al Pent&aacute;gono, el Secretario de Estado, Colin Powell, dijo, "De nuevo, vemos terrorismo, vemos terroristas, personas que no creen en la democracia, personas que sienten que con la destrucci&oacute;n de edificios, con el asesinato de personas, ellos pueden de alguna manera lograr alg&uacute;n prop&oacute;sito pol&iacute;tico".</P>
<P>&#9;Si nos fijamos en la frase, "personas que sienten que con la destrucci&oacute;n de edificios, con el asesinato de personas, ellos pueden de alguna manera lograr alg&uacute;n prop&oacute;sito pol&iacute;tico" estamos leyendo una descripci&oacute;n apta del mismo Colin Powell y de las personas con y para quien el trabaja. </P>
<P>Espec&iacute;ficamente, es una muy buena descripci&oacute;n de lo que &eacute;l y sus colegas--incluyendo a Dick Cheney y a George Herbert Walker Bush--hicieron durante la Guerra del Golfo, la cual despu&eacute;s de todo es la raz&oacute;n por la que Colin Powell sea tan prominente hoy en d&iacute;a. </P>
<P>El se convirti&oacute; en una superestrella de los medios de comunicaci&oacute;n supervisando una guerra que--en menos de dos meses, de acuerdo al Pent&aacute;gono mismo--mat&oacute; alrededor de 200,000 personas con exactamente la misma orientaci&oacute;n fundamental que Powell conden&oacute; inmediatamente despu&eacute;s de que sucedi&oacute; la tragedia.</P>
<P>Lo que est&aacute; en la agenda de los que la monopolizan es el redoblar los tambores de guerra con el pretexto de represalia.</P>

