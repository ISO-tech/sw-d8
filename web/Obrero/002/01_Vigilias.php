<P><font face="Times New Roman, Times, serif" size="5"><b>Miles asisten a vigilias clamando por paz</b></font> </P>
<p><font face="Arial, Helvetica, sans-serif" size="2">14 de Septiembre de 2001 | P&aacute;gina 1</font></p>

<P><font face="Times New Roman, Times, serif" size="3">MIENTRAS LOS pol&iacute;ticos claman por la guerra, miles de personas asistieron a vigilias a trav&eacute;s del pa&iacute;s en duelo por las v&iacute;ctimas de los ataques en Nueva York y Washington--y para demostrar su oposici&oacute;n a m&aacute;s violencia. </P>
<P>En Berkeley, unos 4,000 estudiantes de la Universidad de California asistieron a una "vigilia de libertad de expresi&oacute;n por la paz" poco despu&eacute;s de los ataques. </P>
<P>M&aacute;s de 50 estudiantes y miembros de la comunidad hablaron por micr&oacute;fono a los asistentes. Algunos hablaron acerca de gente que ellos conoc&iacute;an que muri&oacute; en los ataques, y unos pocos apoyaron el llamado del Presidente Bush a la guerra. Pero la inmensa mayor&iacute;a habl&oacute; contra una respuesta violenta de los EE.UU. y contra la reacci&oacute;n racista contra &aacute;rabes y musulmanes. </P>
<P>Despu&eacute;s de que un estudiante llam&oacute; los ataques un "nuevo Pearl Harbor" e inst&oacute; a la muchedumbre a prepararse para la guerra, el pr&oacute;ximo orador dijo, "&#191;Recuerdan c&oacute;mo Pearl harbor finaliz&oacute;? Con la incineraci&oacute;n de 100,000 hombres, mujeres y ni&ntilde;os en Nagasaki e Hiroshima". </P>
<P>En la Universidad de Iowa en Iowa City, casi 1,000 se sentaron silenciosamente sosteniendo velas y escuchando las declaraciones breves en una vigilia la misma noche. Un pu&ntilde;ado de estudiantes que clamaban por la guerra contra las naciones &aacute;rabes gritaron tanto que no dejaron o&iacute;r a otros oradores. Pero un activista respondi&oacute; leyendo un mensaje desagradable puesto en el cub&iacute;culo de su grupo implicando que los activistas estar&iacute;an contentos con los ataques. </P>
<P>"No estamos contentos, nosotros estamos horrorizados", &eacute;l dijo, y fue recibido con un fuerte aplauso. "Nosotros estamos en contra del terrorismo, y en contra de que Bush use esta tragedia para hacer chivos expiatorios de las minor&iacute;as &aacute;rabes". </P>
<P>Cientos se reunieron en vigilias en Nueva York el d&iacute;a despu&eacute;s de los ataques. Y en Chicago, una vigilia convocada por el Reverendo Jesse Jackson fue concurrida por unas 120 personas. </P>
<P>Los pol&iacute;ticos y los sabelotodos de la prensa empujar&aacute;n el patriotismo. Pero estas concurrencias demuestran que no todos est&aacute;n listos para apoyar el llamado de Bush a la guerra y la creciente reacci&oacute;n racista. </P>

