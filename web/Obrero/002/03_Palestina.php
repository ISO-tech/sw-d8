<P><font face="Times New Roman, Times, serif" size="5"><b>Como los EE.UU. apoya la violencia contra Palestina</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">14 de Septiembre de 2001 | P&aacute;gina 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">A POCAS horas del ataque contra el World Trade Center, los medios de televisi&oacute;n ya ten&iacute;an una idea de quienes deb&iacute;an de ser los perpetradores: Los &Aacute;rabes.</P>
<P>Osama bin Laden es el primero en la lista de los sospechosos sin tomar en cuenta que &eacute;l neg&oacute; cualquier conecci&oacute;n con los ataques. Pero los pol&iacute;ticos r&aacute;pidamente culparon a los palestinos tambi&eacute;n&#150;de nuevo, a pesar del hecho de que el l&iacute;der de la Autoridad Palestina, Yasser Arafat y todas las organizaciones palestinas condenaron el ataque.</P>
<P>Mientras, casi todas las estaciones de radio y televisi&oacute;n describ&iacute;an a los palestinos en los Territorios Ocupados, celebrando la tragedia de lo ocurrido en los EE.UU.  </P>
<P>"Yo quisiera enfatizar que el pueblo palestino se identifica con las v&iacute;ctimas de este horrible acto terrorista", dijo Ghassan Al Khattib, director del Centro de Comunicaciones y Prensa de Jerusal&eacute;n.  " Nosotros los palestinos somos v&iacute;ctimas, as&iacute; que tambi&eacute;n sentimos el dolor del pueblo americano&#133;Por supuesto, hubo unas insignificantes exepciones donde algunas personas tontamente expresaron alegr&iacute;a como una reacci&oacute;n inmediata.  La mayor&iacute;a, sin embargo, sinti&oacute; gran tristeza".</P>
<P>Pero el ej&eacute;rcito israel&iacute; no perdi&oacute; tiempo en explotar la tragedia.  </P>
<P>El d&iacute;a despu&eacute;s del ataque, Israel increment&oacute; su represi&oacute;n bombardeando un pueblo en el Banco Occidental y dos villas cercanas, matando a 7 palestinos, incluyendo a una ni&ntilde;a de 11 a&ntilde;os.</P>
<P>"Ahora el mundo no va ser tan paciente como antes ante el terror conducido y apoyado por  la Autoridad Palestina", proclam&oacute; el portavoz del Ministerio de Defensa israel&iacute;, Yarden Vatikay.  En realidad, el ejercito israel&iacute; y los colonos de derecha son la verdadera fuente del terror en el Medio Oriente&#150;con el completo apoyo del gobierno de los EE.UU.</P>
<P>Despu&eacute;s de todo, son los armamentos fabricados en los EE.UU. los que le dan la fuerza abrumadora a Israel con la cual ha mutilado y herido severamente a miles de palestinos&#150;y matado a m&aacute;s de 500&#150;desde hace un a&ntilde;o cuando comenz&oacute; la <I>Intifada</I> [ el levantamiento palestino] contra la   ocupaci&oacute;n por Israel.  </P>
<P>Israel usa aviones y equipos militares estadounidenses para llevar acabo los asesinatos de palestinos&#150;en nombre de la "lucha contra el terrorismo".  </P>
<P>Y es el apoyo pol&iacute;tico y diplom&aacute;tico de los EE.UU. el que ha permitido que el gobierno de Israel a&iacute;sle a los pueblos y a las villas palestinas, acciones que han sumergido a casi la mitad de la poblaci&oacute;n en la extrema pobreza.</P>
<P>El apoyo de los EE.UU. a Israel ha creado amargura y desesperaci&oacute;n en una escala masiva entre los 2 millones de palestinos que viven bajo condiciones parecidas al estado de "apartheid" [como exist&iacute;a antes en Africa del Sur].  </P>
<P>Los palestinos correctamente culpan al gobierno de los EE.UU. por la violencia y la miseria que enfrentan.</P>
<P>Para otros en el Medio Oriente que han experimentado devastaciones similares a manos de los EE.UU. y sus aliados, el impacto de esta tragedia se complica por miedo de que el gobierno de los EE.UU. pueda usarla como una excusa para desatar m&aacute;s destrucci&oacute;n.</P>

