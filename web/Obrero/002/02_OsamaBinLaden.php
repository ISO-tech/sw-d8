<P><font face="Times New Roman, Times, serif" size="5"><b>Como la CIA entren&oacute; a Osama bin Laden</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">14 de Septiembre de 2001 | P&aacute;gina 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3">EL MILLONARIO hombre de negocios saudita, Osama bin Laden y su "vasta red" de terroristas internacionales fueron culpados r&aacute;pidamente por el ataque a&eacute;reo a Nueva York y Washington, D.C.</P>
<P>Esto no es nada nuevo. Bin Laden ha sido por a&ntilde;os el cuco favorito del establecimiento pol&iacute;tico de Washington. El ha sido culpado por el ataque suicida a un destructor estadounidense en Yemen el a&ntilde;o pasado, los bombasos de dos embajadas norteamericanas en Africa en1998 y m&aacute;s.  </P>
<P>Pero si bin Laden est&aacute; envuelto en terrorismo, &eacute;l fue educado por los mejores&#150;el gobierno de los Estados Unidos.</P>
<P>Los dos, &eacute;l y el Talib&aacute;n de Afganist&aacute;n son producto de diez a&ntilde;os de guerra apoyada por los EE.UU. contra la ocupaci&oacute;n de Afganist&aacute;n por la ex-Uni&oacute;n Sovi&eacute;tica. Despu&eacute;s de que las tropas rusas invadieron Afganist&aacute;n en 1979, los EE.UU. entrenaron a bin Laden y a miles de hombres &aacute;rabes.  </P>
<P>El aquel entonces presidente de los EE.UU., Ronald Reagan, gustaba llamar a bin Laden y a sus secuaces "luchadores por la libertad".</P>
<P>Despu&eacute;s de que la Uni&oacute;n Sovi&eacute;tica se vio forzada a salir de Afganist&aacute;n en 1989, los "luchadores por la libertad" entrenados por la CIA, se dividieron en facciones rivales que lucharon en una guerra civil durante la d&eacute;cada de los noventa. Con la ayuda de la CIA y el aliado de EE.UU, Arabia Saudita, el peque&ntilde;o grupo de la milicia Taliban, emergi&oacute; del caos, apoder&aacute;ndose del gobierno en 1996.  </P>
<P>Los EE.UU. apoyaron a los islamistas de l&iacute;nea dura del Talib&aacute;n porque ellos pensaron que el grupo podr&iacute;a proveer estabilidad para los grandes negocios. Pero entonces el Talib&aacute;n empez&oacute; a brindar refugio a bin Laden y a otros movimientos isl&aacute;micos a los cuales los EE.UU. se oponen. </P>
<P>Como Saddam Hussein en Irak antes que ellos, bin Laden y el Talib&aacute;n han pasado de aliados a ser enemigos de Estados Unidos.</P>

