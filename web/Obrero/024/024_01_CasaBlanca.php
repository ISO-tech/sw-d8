<font face="Times New Roman, Times, serif" size="5"><b>Una Casa Blanca de espaldas a la pared</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">noviembre-diciembre de 2005 | p&aacute;gina 1</font><p>

<font face="Times New Roman, Times, serif" size="3">Parec&iacute;a como si un terremoto hubiese azotado a Washington. Cuando el congresista Dem&oacute;crata y halc&oacute;n John Murtha en noviembre anunci&oacute; su posici&oacute;n a favor de la retirada de las tropas estadounidenses de Irak, a los Republicanos les dio una rabieta.<p>
Acusaron a Murtha de traicionar a las tropas. Poco despu&eacute;s, propusieron una resoluci&oacute;n para que las tropas se retiraran de Irak inmediatamente. Los Dem&oacute;cratas, mostrando su oposici&oacute;n fantoche contra la guerra, se sumaron a los Republicanos para derrotar abrumadoramente la resoluci&oacute;n.<p>
A pesar de que la propuesta de Murtha no tiene nada que ver con la postura del movimiento antiguerra, muchos Dem&oacute;cratas no la apoyaron porque no quieren ser vistos como "antiguerra". Y no olvidemos que muchos de ellos apoyan la ocupaci&oacute;n de Irak.<p>
Sin embargo, ahora no es "inconcebible" que los l&iacute;deres pol&iacute;ticos reconozcan que la guerra en Irak es un caso perdido. Lo que se debe a la creciente oposici&oacute;n contra Bush por parte de la gente com&uacute;n. Pese a la timidez de los Dem&oacute;cratas, la ira contra la administraci&oacute;n de Bush y las prioridades reaccionarias de Washington est&aacute; expres&aacute;ndose de una forma u otra.<p>
La Casa Blanca est&aacute; hundida en esc&aacute;ndalos hasta el cuello y demostr&oacute; su incompetencia y su indiferencia hacia el pueblo trabajador con su inacci&oacute;n tras el Hurac&aacute;n Katrina.<p>
No obstante, la cuesti&oacute;n m&aacute;s importante es la guerra porque Bush decidi&oacute; ponerla en el centro de su gesti&oacute;n. Las encuestas m&aacute;s recientes muestran que el 59 por ciento de la gente cree que la invasi&oacute;n fue un error, y una cantidad m&aacute;s numerosa quiere que regresen a casa inmediatamente o todas o una parte substancial de las tropas.<p>
El movimiento antiguerra ha revivido substancialmente coincidiendo con la vigilia de Cindy Sheehan frente al rancho de Bush en Texas durante sus vacaciones, y floreci&oacute; en las manifestaciones antiguerra de septiembre y en el renovado movimiento estudiantil en contra de los reclutadores militares en sus escuelas.<p>
La lucha contra el reclutamiento militar tiene repercusiones, desde impedir la presencia militar en las escuelas y hacer bajar los niveles de alistamiento en las fuerzas armadas, hasta el triunfo del refer&eacute;ndum College Not Combat ("Educaci&oacute;n universitaria en vez de combate") en San Francisco en noviembre.<p>
Mientras tanto, quienes est&aacute;n interesados en oponerse al racismo y en defender los derechos civiles se est&aacute;n organizando en torno a otra importante lucha--tratar de impedir que Stan Tookie Williams, un prisionero en el pabell&oacute;n de la muerte de California, sea ejecutado. La contienda para ejecutar a este ex-pandillero que se convirti&oacute; en conciliador entre gangas muestra a todo el mundo porque la pena de muerte es racista, irracional y discriminatoria contra los pobres.<p>
La administraci&oacute;n de Bush est&aacute; arrinconada y es dif&iacute;cil concebir c&oacute;mo pueda recuperarse. Pero las lecciones de la historia nos demuestran que la clase dominante puede recuperarse de cualquier crisis, cuando recurre a depender de su segundo equipo, los Dem&oacute;cratas, una vez los Republicanos han perdido su credibilidad--especialmente si no hay oposici&oacute;n de base para confrontarla.<p>
Este tipo de oposici&oacute;n debe ser construida a partir de cada lucha, por menor que sea. Pero la posibilidad de que surja esta lucha generalizada es patente en este momento.<p>
No hay tiempo que perder. Tenemos que lanzarnos de frente y envolvernos en toda lucha que se oponga a las prioridades conservadoras de la clase dominante. Y en este proceso, tenemos que construir una nueva izquierda que pueda convertirse en el eje de todas las luchas que surjan.<p>

