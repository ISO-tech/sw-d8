<P><font face="Times New Roman, Times, serif" size="4">Pol&iacute;tica de inmigraci&oacute;n otra vez bajo la lupa en Arizona</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A luchar en contra de la Proposici&oacute;n 200</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Por Jeff Bale</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | 24 de septiembre de 2004 | p&aacute;gina 2</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>Dado el aumento en la vigilancia--hay hasta murallas--en las fronteras con California y Tejas, muchos inmigrantes que entran a EE.UU. sin papeles tienen que hacerlo a trav&eacute;s del desierto entre Sonora, M&eacute;xico y Arizona, donde mueren miles de personas cada a&ntilde;o por el calor y la deshidrataci&oacute;n en su b&uacute;squeda de una mejor vida.</P>
<P>Por eso la inmigraci&oacute;n se ha vuelto un asunto aun m&aacute;s pol&eacute;mico en este estado en los &uacute;ltimos a&ntilde;os. Este a&ntilde;o los racistas asestaron el primer golpe. Ellos han conseguido incluir la Proposici&oacute;n 200 en la papeleta de las elecciones de noviembre, bajo el descarado nombre de "Protect Arizona Now" (Protege a Arizona Ahora). </P>
<P>Esta proposici&oacute;n obliga a toda persona a probar que es un ciudadano legal para poder votar y recibir servicios del gobierno. Sus partidarios dicen que este requisito evita el fraude electoral y el abuso de los servicios p&uacute;blicos por inmigrantes indocumentados.</P>
<P>Los conservadores se est&aacute;n aprovechando de las im&aacute;genes estereotipadas de siempre, de inmigrantes como los responsables de todo lo malo en nuestra sociedad--de la criminalidad, del abuso de los servicios p&uacute;blicos, del "robo" de empleos a los "americanos", etc.--para ganar m&aacute;s apoyo. Y como siempre, ellos simplemente mienten. Estudio tras estudio prueba que los inmigrantes indocumentados contribuyen m&aacute;s a la econom&iacute;a en servicios prove&iacute;dos y en impuestos pagados que lo que reciben a cambio del gobierno.</P>
<P>Adem&aacute;s, el &uacute;nico fraude electoral perpetrado en este ciclo electoral es el que cometieron los racistas mismos para lograr incluir la Proposici&oacute;n 200 en la votaci&oacute;n general. Cuando estaban solicitando firmas de los electores, los partidarios de la 200 no dijeron nada sobre los requisitos espec&iacute;ficos que impondr&iacute;a la nueva ley. Por ejemplo, que si un empleado del gobierno se da cuenta de que uno de los clientes que est&aacute; ayudando no es ciudadano, <I>el empleado</I> puede ser despedido, arrestado y enjuiciado.</P>
<P>Los liberales comenzaron su propia campana en contra de la Proposici&oacute;n 200 solo en septiembre. Apoyados por varias uniones, como la de los bomberos y SEIU, hicieron una conferencia de prensa para anunciar su campa&ntilde;a. Por desgracia, ellos nombraron su campa&ntilde;a "Arizone&ntilde;os por la reforma inmigratoria real". Es decir, ellos tambi&eacute;n creen que hay un "problema" con la inmigraci&oacute;n, solo que ellos buscan una soluci&oacute;n m&aacute;s amable y no tan reaccionaria.</P>
<P>El resultado de esto es que la encuesta m&aacute;s reciente sobre la 200 revela que un 66 por ciento de los votantes la apoyan--y mucho peor, &#161;que un 59 por ciento apoya el requisito que obliga a los empleados p&uacute;blicos a reportar a los inmigrantes indocumentados que est&eacute;n recibiendo cualquier servicio del gobierno!</P>
<P>Luchar en contra de la Proposici&oacute;n 200 concediendo que "hay un problema con la inmigraci&oacute;n" no ser&aacute; suficiente para derrotar a la proposici&oacute;n, ni a los racistas que la apoyan.</P>

