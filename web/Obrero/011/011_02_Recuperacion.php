<P><font face="Arial, Helvetica, sans-serif" size="2"><b>LO QUE PENSAMOS</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>No hay recuperaci&oacute;n para los trabajadores</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">15 de augusto de 2003 | P&aacute;gina 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3">"NUESTRA ECONOMIA claramente se est&aacute; moviendo en la direcci&oacute;n correcta", el Secretario de Comercio Donald Evans declar&oacute; recientemente.</P>
<P>Puede que sea "la direcci&oacute;n correcta", pero para las corporaciones americanas. Para los trabajadores--quienes siguen sufriendo despidos masivos, largos per&iacute;odos sin trabajo, y sueldos y beneficios que siguen cayendo--es dif&iacute;cil ver mejora.</P>
<P>Los muy bien pagados analistas de Wall Street celebraron despu&eacute;s del anuncio Evans que el producto interno bruto (PIB) de EE.UU. aument&oacute; a una tasa anual de 2.4 por ciento desde abril a junio, casi el doble de la expansi&oacute;n en los tres meses anteriores. Pero hay varios factores que necesitan ser considerados para obtener un cuadro m&aacute;s claro de lo que est&aacute; pasando. </P>
<P>En primer lugar, m&aacute;s de dos tercios del crecimiento del PIB se debi&oacute; al aumento masivo de los gastos militares a causa de la guerra contra Irak. El crecimiento en los gastos militares fue el m&aacute;s grande desde 1951, en tiempos de la guerra contra Corea. No es necesario decir que el est&iacute;mulo econ&oacute;mico que la guerra proporcion&oacute; no continuar&aacute; al mismo nivel. </P>
<P>Las estad&iacute;sticas mostraron tambi&eacute;n una mejora en las inversiones burs&aacute;tiles, seg&uacute;n el Departamento del Trabajo. Pero tambi&eacute;n aqu&iacute; los resultados son contradictorios.</P>
<P>Lo importante es que el crecimiento que est&aacute; afectando a la econom&iacute;a no es bastante para revertir el factor que afecta a los trabajadores m&aacute;s profundamente--el desempleo. La tasa de desempleo baj&oacute; en julio, seg&uacute;n el Departamento del Trabajo. Pero este resultado es enga&ntilde;oso porque se debe a que medio mill&oacute;n de desempleados abandonaron su b&uacute;squeda de trabajo y por lo tanto desaparecieron de las estad&iacute;sticas.</P>
<P>&#191;C&oacute;mo puede la situaci&oacute;n laboral permanecer tan miserable mientras que las estad&iacute;sticas econ&oacute;micas parecen estar mejorando? Una respuesta inmediata es que las corporaciones americanas est&aacute;n usando la recesi&oacute;n y el desempleo para presionar a los trabajadores a trabajar y producir m&aacute;s por menos.</P>
<P>Los economistas estiman que la productividad laboral--la medida del producto total por hora--aument&oacute; de abril a junio a una tasa que es el doble del incremento en el PIB.</P>
<P>Los efectos de esta campa&ntilde;a para elevar la productividad se pueden ver a trav&eacute;s de la econom&iacute;a, pues en cada lugar de trabajo los jefes exhortan a los trabajadores a "sacrificarse" y trabajar m&aacute;s para enfrentar los "tiempos dificiles"--como el conflicto laboral en Verizon. Pero "el sacrificio" nunca es equitativo.</P>
<P>No nos sorprendamos de ver m&aacute;s estad&iacute;sticas econ&oacute;micas favorables en los meses venideros. El regalo que la administraci&oacute;n Bush dio a los ricos con el recorte de impuestos est&aacute; inyectando dinero al sistema, y aunque los cr&iacute;ticos tienen raz&oacute;n cuando dicen que la mayor&iacute;a del dinero acabar&aacute; en las cuentas bancarias de los ricos, &eacute;ste tendr&aacute; alg&uacute;n impacto.</P>
<P>La Casa Blanca est&aacute; determinada a producir una recuperaci&oacute;n econ&oacute;mica de corto plazo, justo a tiempo para las elecciones del pr&oacute;ximo a&ntilde;o. Pero incluso si en esto es exitosa, los problemas a largo plazo van a permanecer --sobre todo, altos niveles de sobrecapacidad y deuda corporativa, lo que continuar&aacute; presionando la c&aacute;ida de la inversi&oacute;n y obstaculizar&aacute; la expansi&oacute;n econ&oacute;mica que producir&iacute;a nuevos empleos. </P>
<P>La verdad es cruel. Las corporaciones pueden salir de cualquier crisis si pueden forzar a los trabajadores a pagar el precio. Y as&iacute; lo est&aacute;n haciendo otra vez--y ese es el verdadero sentido de las estad&iacute;sticas econ&oacute;micas que los analistas de Wall Street celebraron euf&oacute;ricamente.</P>

