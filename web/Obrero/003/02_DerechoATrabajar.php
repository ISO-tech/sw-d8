<P><font face="Times New Roman, Times, serif" size="5"><b>Apoyemos el derecho a trabajar</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Por Eduardo Capulong</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | 8 de febrero de 2002 | P&aacute;gina 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SAN FRANCISCO--Justo cuando sus trabajos estaban a punto de mejorar, los trabajadores inmigrantes que trabajan en seguridad revisando a los pasajeros en los aeropuertos (inspectores) enfrentan la posibilidad de perder sus empleos.</P>
<P>La legislaci&oacute;n federal aprobada el 19 de noviembre va a nacionalizar la seguridad de los aeropuertos, creando 28,000 nuevos trabajos federales con un salario anual de casi US$35,000 m&aacute;s beneficios.</P>
<P>Pero debido a la histeria anti-inmigrante, alimentada por el Congreso y la administraci&oacute;n de Bush, solo ciudadanos podr&aacute;n obtener uno de estos empleos. A menos que la ley sea enmendada, del 25 al 85 por ciento de estos trabajadores perder&aacute;n sus trabajos a mediados de febrero--simplemente porque son inmigrantes.</P>
<P>Cerca del 90 por ciento de los inspectores en San Francisco son filipinos, muchos de los cuales han trabajado all&iacute; por m&aacute;s de 10 a&ntilde;os, de acuerdo con Luisa Blue, presidenta nacional de la Alianza Laboral Asi&aacute;tico-Pac&iacute;fico-Americana. Los activistas han descrito la ley como la "limpieza &eacute;tnica de filipinos a trav&eacute;s de despidos masivos".</P>
<P>El requerimiento de ciudadan&iacute;a no tiene nada que ver con la seguridad en los aeropuertos. Los pilotos, asistentes de vuelo y otros trabajadores en los aeropuertos, incluyendo a la Guardia Nacional ahora estacionada ah&iacute;, no tienen que ser ciudadanos.</P>
<P>Necesitamos exigir que la ley sea cambiada para permitir que los trabajadores inmigrantes preserven sus trabajos y su membres&iacute;a en los sindicatos.</P>

