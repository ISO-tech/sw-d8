<P><font face="Times New Roman, Times, serif" size="5"><b>"El pueblo debe conocer la verdad"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">8 de febrero de 2002 | P&aacute;gina 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>PEDRO DIAZ era jefe de la Unidad de Derechos Humanos de la Fiscal&iacute;a en Colombia. Despu&eacute;s de recibir numerosas amenazas de muerte, &eacute;l tuvo que huir el pa&iacute;s en 2001. BRIDGET BRODERICK de <I>Obrero Socialista</I> le entrevist&oacute; en la manifestaci&oacute;n contra La Escuela de las Am&eacute;ricas en Georgia el noviembre pasado.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><B><P>&#191;C&Oacute;MO EL Plan Colombia afecta al pueblo colombiano?</P>
</B><P>COMO EL Plan Colombia tiene un alto componente militar de apoyo, los militares que van a estar en el terreno en acciones contra grupos guerrilleros, que a veces controlan algunos sectores y contra personas civiles, los excesos que producen las actuaciones de los militares contra la poblaci&oacute;n civil deben ser y son juzgados, son investigados y juzgados por miembros de la fiscal&iacute;a de la unidad de derechos humanos.</P>
<P>Y se ha intentado de que en esas ayudas de Plan Colombia hay una ayuda especial para la administraci&oacute;n de justicia. Se prometi&oacute; para la unidad, por ejemplo, derechos humanos en el Plan Colombia de 2001, $25 millones de d&oacute;lares. S&oacute;lo se han hecho desembolsos hasta ahora, parecen no arriba de $1 mill&oacute;n de d&oacute;lares.</P>
<P>Mientras que la ayuda militar est&aacute; agotada.</P>
<B><P>&#191;DE QU&Eacute; manera han interferido las corporaciones americanas con el pueblo colombiano y su lucha por los derechos humanos?</P>
</B><P>HAY ALGUNAS investigaciones. Yo conoc&iacute; de investigaciones en las cuales se se&ntilde;alaba que en homicidios de dirigentes sindicales de Coca Cola Colombia [y otros], hubo alguna intervenci&oacute;n for&aacute;nea.</P>
<P>Esa situaci&oacute;n es muy grave porque en este momento m&aacute;s de 125 dirigentes sindicales han sido asesinados este a&ntilde;o [2001]. Eso complica mucho la situaci&oacute;n.</P>
<P>La mayor&iacute;a de estas personas han pertenecido a empresas extranjeras, como Ladrumo, que explota el carb&oacute;n en el Departamento de C&eacute;sar.</P>
<P>Varios de estos sindicalistas y l&iacute;deres sindicalistas pertenecieron al sindicato de Coca Cola, por ejemplo. Pues, alguna conexi&oacute;n tendr&iacute;a que haber.</P>
<B><P>&#191;TIENEN LOS militares alguna conexi&oacute;n?</P>
</B><P>ESTO ES homicidio de los dirigentes laborales. En su gran mayor&iacute;a, est&aacute;n relacionados con grupos paramilitares y operan en las regiones donde est&aacute;n los sindicatos y la actividad sindical.</P>
<P>Eso es obvio, se han producido investigaciones, se han producido actas de acusaciones, se han producido sentencias contra grupos paramilitares. Y estos grupos paramilitares operan con conocimiento de las brigadas militares y de polic&iacute;a que est&aacute;n en la zona.</P>
<B><P>&#191;C&Oacute;MO PODEMOS nosotros ayudar a los colombianos?</P>
</B><P>DEBE CONOCER la situaci&oacute;n <i>real,</i> no oficial, la situaci&oacute;n que ense&ntilde;an los medios de comunicaci&oacute;n.</P>
<P>Se debe enterar de adonde va el dinero de sus impuestos, que es un invertido en estos planes, adonde va la ayuda militar, y que se est&aacute; haciendo con esto&#133; Y una lucha contra la droga militar no es esa.</P>
<P>Pero primero el pueblo debe conocer c&oacute;mo le est&aacute; haciendo ac&aacute;, cu&aacute;l es la realidad del pa&iacute;s. Mas all&aacute; de la informaci&oacute;n oficial--eso es lo primero, cuando el ciudadano norteamericano conozca, seguramente, se produce una reacci&oacute;n.</P>

