<P><font face="Times New Roman, Times, serif" size="5"><b>&#161;Argentinazo!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">8 de febrero de 2002 | P&aacute;gina 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>Decenas de miles de argentinos se tomaron las calles a finales de enero en la primera gran demostraci&oacute;n desde la masivas protestas que derrocaron dos gobiernos en diciembre.</P>
<P>El descontento nacional mostr&oacute; la continua furia que los trabajadores y los pobres de Argentina tienen contra las dur&iacute;simas medidas neoliberales llevadas a cabo por dos presidentes que fueron forzados a renunciar. Todav&iacute;a son apoyadas por el tercer gobierno de Eduardo Duhalde.</P>
<P>En todas las grandes ciudades hubo cacerolazos y grandes demostraciones. En la capital, Buenos Aires, la polic&iacute;a atac&oacute; una demostraci&oacute;n pac&iacute;fica, disparando r&aacute;fagas de balas de goma y confront&aacute;ndose con los manifestantes por horas.</P>
<P>Los manifestantes demostraron que el esp&iacute;ritu del Argentinazo de diciembre est&aacute; aun en pie y saludable.</P>
<P>Durante los primeros a&ntilde;os de la d&eacute;cada de los 90, Argentina fue el alumno preferido de las pol&iacute;ticas econ&oacute;micas neoliberales promovidas por el gobierno de los Estados Unidos y sus sirvientes en el Fondo Monetario Internacional y en el Banco Mundial.</P>
<P>Las meras pol&iacute;ticas de libre comercio, privatizaciones, desregulaci&oacute;n y austeridad son las que llevaron a Argentina a esta terrible crisis econ&oacute;mica. Pero m&aacute;s temible para Washington es el ejemplo dado por los argentinos que el desvelamiento de su agenda econ&oacute;mica.</P>
<P>Hartos de un gobierno cuya &uacute;nica respuesta a la recesi&oacute;n fue m&aacute;s austeridad, las masas se levantaron y dijeron NO por medio de masivas protestas por todo el pa&iacute;s que culminaron en m&aacute;s de un mill&oacute;n copando las calles de Buenos Aires.</P>
<P>El ex-Presidente Fernando de la R&uacute;a trat&oacute; de organizar la represi&oacute;n, pero esto no pudo detener las protestas y su gobierno fue forzado a renunciar el 20 de diciembre, para ser seguido en la misma suerte por un segundo gobierno una semana m&aacute;s tarde. Esto da un magn&iacute;fico ejemplo a los trabajadores alrededor del mundo.</P>
<P>Pero es obvio que el gobierno de Duhalde, que clama ser partidario del cambio, est&aacute; determinado a resolver la crisis sobre la espalda de los trabajadores.</P>
<P>La alternativa existe. Las protestas de diciembre fueron el resultado de a&ntilde;os de organizaci&oacute;n, principalmente dirigidas por los desempleados, quienes desarrollaron la t&aacute;ctica de bloquear carreteras para arrancarles programas de emergencia para alimentos y trabajo a las autoridades.</P>
<P>Estos piqueteros han comenzado a juntarse en coordinaciones nacionales de organizaciones locales, quienes tienen el poder de tomar en sus propias manos la distribuci&oacute;n de comida y otros servicios. Y los trabajadores argentinos, cuyas huelgas generales han paralizado el pa&iacute;s siete veces en dos a&ntilde;os, pueden sobrepasar a sus l&iacute;deres sindicales conservadores y ocupar sus f&aacute;bricas y oficinas.</P>
<P>La lucha en Argentina est&aacute; mostrando la alternativa a un mundo de miseria y sufrimiento--la lucha por transformar el sistema en uno organizado para cubrir las necesidades de todos y no las demandas de los banqueros y las multinacionales.</P>

