<font face="Times New Roman, Times, serif" size="5"><b>Aliado de Bush pierde</b></font></P>
<P><font face="Arial, Helvetica, sans-serif" size="2">19 de marzo de 2004 | p&aacute;gina 3</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>LA ESPECTACULAR derrota electoral del gobierno conservador espa&ntilde;ol despu&eacute;s de los recientes atentados terroristas en Madrid ha demostrado que la mayor&iacute;a del pueblo rechaz&oacute; el intento de los conservadores de manipular la traged&iacute;a para su propia ventaja.</P>
<P>El gobierno del primer ministro Jos&eacute; Maria Aznar declar&oacute; justo despu&eacute;s de los ataques que el grupo vasco, la ETA, era responsable por las bombas que mataron a m&aacute;s de 200 personas el 11 de marzo en Madrid. De hecho, el gobierno ten&iacute;a informaci&oacute;n de que un grupo aliado con Al-Qaeda plant&oacute; las bombas--e ignor&oacute; la evidencia de que los ataques no correspond&iacute;an a los m&eacute;todos usados por la ETA, un grupo ilegal que exige la independencia del pa&iacute;s vasco.</P>
<P>Al-Qaeda anunci&oacute; que las bombas fueron en represalia por el papel de Espa&ntilde;a en la guerra y la ocupaci&oacute;n de Irak--una guerra a la que se opuso el 90 por ciento de la poblaci&oacute;n espa&ntilde;ola. Tales ataques contra la gente com&uacute;n--mayoritariamente obreros, inmigrantes y estudiantes--en un pa&iacute;s cuya poblaci&oacute;n se opuso masivamente a la guerra--son indefensibles. Estas pol&iacute;ticas son desastrosas.</P>
<P>Pero Aznar y su sucesor como l&iacute;der del Partido Popular (PP), Mariano Rajoy, trataron de enga&ntilde;ar a los electores intentando convertir a la ETA en chivos expiatorios. El PP perdi&oacute; no s&oacute;lo a causa de una guerra detestada, pero tambi&eacute;n por seguir empujando un programa para fortalecer el estado central, limitar el poder de los gobiernos regionales, e imponer restricciones contra los inmigrantes. </P>
<P>Para el PP, estos atentados no fueron una traged&iacute;a, sino una oportunidad. Seg&uacute;n declar&oacute; el grupo izquierdista espa&ntilde;ol, Espacio Alternativo, "Aznar aprovechaba as&iacute; la indignaci&oacute;n ciudadana para llamar a cerrar filas y a movilizarse en torno a la defensa de la Constituci&oacute;n, anunciando futuras medidas de restricci&oacute;n de libertades y derechos b&aacute;sicos en nombre de la lucha contra el 'terrorismo'".</P>
<P>Este proyecto conservador incluye reformas econ&oacute;micas "neoliberales"--privatizaci&oacute;n, desregulaci&oacute;n y recortes en los programas sociales--y la ilegalizaci&oacute;n de los grupos pol&iacute;ticos que representan a las minor&iacute;as nacionales.</P>
<P>Sin embargo, antes de que ocurrieran estos ataques, se esperaba que el PP ganara las elecciones. Pero despu&eacute;s de las revelaci&oacute;nes de que el gobierno minti&oacute; sobre el papel de la ETA, mucha gente que particip&oacute; en las manifestaci&oacute;nes convocadas por el gobierno contra "el terror" se uni&oacute; a las protestas contra el PP el d&iacute;a antes de las elecciones del 14 de marzo.</P>
<P>La derrota de los conservadores espa&ntilde;oles es un vict&oacute;ria para el movimiento mundial contra la guerra. Pero el ganador de las elecciones, el Partido Socialista Obrero Espa&ntilde;ol (PSOE), no se opone al imperialismo. Durante la guerra del Golfo P&eacute;rsico de 1991, el gobierno del PSOE presidido por Felipe Gonz&aacute;lez permiti&oacute; que las fuerzas armadas de EE.UU. usaran las facilidades militares espa&ntilde;olas para bombardear a Irak--y mand&oacute; buques de guerra para bloquear a ese pa&iacute;s. El movimiento anti-guerra espa&ntilde;ol debe continuar su lucha.</P>
<P>Sin embargo, despu&eacute;s de estos eventos en Espa&ntilde;a, ser&aacute; m&aacute;s dificil para George W. Bush y sus aliados el usar al "terrorismo" como justificaci&oacute;n para la ocupaci&oacute;n de Irak. Aqu&iacute; en las entra&ntilde;as de la bestia, tenemos que organizarnos tambi&eacute;n y reenfatizar nuestras demandas: &#161;EE.UU. fuera de Irak--y alto a la ocupaci&oacute;n ahora!</P>

