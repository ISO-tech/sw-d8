<P><font face="Arial, Helvetica, sans-serif" size="2"><b>REP&Uacute;BLICA DOMINICANA</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>La crisis en la Rep&uacute;blica Dominicana profundiza</b></font></P>

<P><font face="Times New Roman, Times, serif" size="2"><b>Por Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | 19 de marzo de 2004</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>OTRO MILAGRO de la econom&iacute;a neoliberal en Latinoam&eacute;rica se ha desmoronado en con una secuela de desplome econ&oacute;mico, protestas masivas y represi&oacute;n--esta vez en la Rep&uacute;blica Dominicana. </P>
<P>Por lo menos dos personas fueron asesinadas y m&aacute;s de 150 fueron detenidas durante los dos d&iacute;as de la huelga general que acaeci&oacute; en enero. Fue una acci&oacute;n convocada para protestar contra la gran tasa de inflaci&oacute;n y las medidas de austeridad que han producido apagones en partes del pa&iacute;s de hasta 20 horas diarias.</P>
<P>Si el Presidente dominicano Hip&oacute;lito Mej&iacute;a se sale con la suya, infligir&aacute; m&aacute;s miseria bajo los t&eacute;rminos de un "rescate" de emergencia del Fondo Monetario Internacional (FMI). El FMI quiere que los trabajadores y los pobres paguen por la crisis--al subir los impuestos del alcohol y del tabaco, cortar el presupuesto gubernamental y limitar los aumentos de sueldo del sector p&uacute;blico al 9 por ciento, a pesar de que la tasa de inflaci&oacute;n fue de un 43 por ciento el a&ntilde;o pasado.</P>
<P>A finales de la d&eacute;cada de los noventa, la Rep&uacute;blica Dominicana era considerada como un modelo econ&oacute;mico para Latinoam&eacute;rica. Ten&iacute;a una tasa de crecimiento que lleg&oacute; al 7 por ciento anual bajo el Presidente anterior, Leonel Fern&aacute;ndez, del Partido de la Liberaci&oacute;n Dominicano (PLD). Los ricos y corruptos funcionarios gubernamentales, as&iacute; como sectores de la clase media prosperaron como nunca antes.</P>
<P>Pero los trabajadores--cuya labor sosten&iacute;a una econom&iacute;a de super-explotaci&oacute;n--no gozaron de los frutos de esta abundancia econ&oacute;mica. Los pobres, desproporcionadamente negros, muchos de origen haitiano, continuaron sufriendo discriminaci&oacute;n racista y condiciones p&eacute;simas mientras que el gasto total en servicios sociales lleg&oacute; a penas al 7 por ciento del Producto Interno Bruto--la mitad del promedio del resto de Latinoam&eacute;rica.</P>
<P>Mej&iacute;a, en su campa&ntilde;a para la presidencia en 2000 como candidato del partido (nominalmente) de centro-izquierda, el Partido Revolucionario Democr&aacute;tico (PRD), tuvo &eacute;xito con la consigna "El pueblo primero" y con la promesa de acabar con la corrupci&oacute;n. Una vez elegido, se convirti&oacute; en un buen amigo de las empresas, incluso de empresarios como Ram&oacute;n B&aacute;ez Figueroa, el jefe del Banco Intercontinental, y de Baninter, el segundo banco m&aacute;s grande del pa&iacute;s. Este hasta le regal&oacute; dos veh&iacute;culos tipo SUV a Mej&iacute;a. </P>
<P>Pero Baninter se declar&oacute; en bancarrota cuando los reguladores descubrieron que cientos de millones de d&oacute;lares poco a poco hab&iacute;an desaparecido misteriosamente del banco. El gobierno intervino para rescatar a los depositantes--inclusive los dep&oacute;sitos de los ricos con cuentas bancarias en el extranjero--duplicando as&iacute; de la noche a la ma&ntilde;ana la deuda de la Rep&uacute;blica Dominicana. </P>
<P>El resultado fue una ca&iacute;da desastrosa de la moneda, el peso. Los precios subieron fuera de control, resultando en los apagones porque el gobierno no pudo pagar las subvenciones que manten&iacute;an la electricidad a un precio manejable. Las pr&oacute;ximas elecciones est&aacute;n programadas para mayo, y se espera que Fern&aacute;ndez derrote a Mej&iacute;a en la contienda. No importa quien gane, lo que se espera son m&aacute;s pol&iacute;ticas de austeridad.</P>
<P>La Rep&uacute;blica Dominicana--como otros pueblos del continente--est&aacute; demostrando el potencial que existe para una forma diferente de hacer pol&iacute;tica en que la gente com&uacute;n interviene en las decisiones y acciones que afectan su futuro, seg&uacute;n fue claramente evidenciado por las valientes acciones populares durante la huelga general de enero.</P>

