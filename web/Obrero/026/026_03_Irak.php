<font face="Times New Roman, Times, serif" size="5"><b>Irak al borde del precipicio</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">marzo-abril de 2006 | p&aacute;gina 3</font><p>

<font face="Times New Roman, Times, serif" size="3">LA BOMBA que estall&oacute; a finales de febrero en la mezquita chi&iacute;ta Askariya, en Samarra, result&oacute; en a una oleada de ataques contra mezquitas sunitas y la muerte de un sinn&uacute;mero de iraqu&iacute;es. La violencia sectaria fue la peor desde la invasi&oacute;n de EEUU en 2003, invocando el espectro de una guerra civil en Irak. En respuesta, las fuerzas del gobierno y los soldados de EEUU arremetieron imponiendo un toque de queda continuo.<p>
Seg&uacute;n ciertos informes, demostraciones conjuntas de sunitas y chi&iacute;tas echaron la culpa por la bomba a quien le corresponde&#8212;a los ocupadores estadounidenses. Esa misma semana, Moktada al-Sadr, el religioso chi&iacute;ta militante cuyos partidarios han chocado peri&oacute;dicamente con las fuerzas armadas de EEUU, repiti&oacute; su demanda por la retirada inmediata de EEUU y convoc&oacute; a actos de oraci&oacute;n conjuntos entre chi&iacute;tas y sunitas y a otras demostraciones de unidad. <p>
Poco antes, Sami Ramadani, un exiliado izquierdista bajo el r&eacute;gimen de Saddam Hussein en Irak y adversario prominente contra la guerra de EEUU, hab&iacute;a descrito las protestas contra la ocupaci&oacute;n en Samarra, Kut, el vecindario "Ciudad Sadr" de Bagdad y en otras partes. <p>
"[E]l estado de &aacute;nimo popular es m&aacute;s bien anti-ocupaci&oacute;n que sectario" escribi&oacute; Ramadani en el peri&oacute;dico <i>Guardian</i> de Gran Breta&ntilde;a. "Irak est&aacute; cundido de rumores acerca de la confabulaci&oacute;n entre las fuerzas de la ocupaci&oacute;n y sus t&iacute;teres iraqu&iacute;es en los ataques sectarios y los escuadrones de la muerte: EEUU es visto ampliamente como fomentando la divisi&oacute;n sectaria para prevenir que surja una resistencia nacional unificada".<p>
Sin embargo, a la misma vez no se debe subestimar la violencia mortal dirigida contra los sunitas. Esta amenaza ha obligado a EEUU a mostrarse m&aacute;s en&eacute;rgico en sus tentativas de limitar el poder de los partidos chi&iacute;tas, quienes han sido expuestos por operar escuadrones de la muerte desde del Ministerio del Interior y de la polic&iacute;a iraqu&iacute;. <p>
La bomba de Samarra estall&oacute; cuando el embajador estadounidense, Zalmay Khalilzad, intentaba empujar a los partidos musulmanes chi&iacute;tas que ganaron en las recientes elecciones de diciembre a que aceptaran un gobierno de "unidad nacional". Seg&uacute;n informes de noticias, Khalilzad quer&iacute;a que el nuevo gobierno incluyera a grupos sunitas y a los dos partidos Kurdos principales&#8212;y que se nombrara a Ayad Allawi (viejo instrumento de la CIA y ex-primer ministro) a un puesto clave en el gabinete de un gobierno provisional.<p>
La meta de EEUU era quitarle el control del influyente Ministerio del Interior al Concilio Supremo para la Revoluci&oacute;n Isl&aacute;mica en Irak, partido chi&iacute;ta apoyado por Ir&aacute;n&#8212;y forzar la retirada del Primer Ministro Ibrahim al-Jaafari, cuyo partido, Dawa, tambi&eacute;n tiene v&iacute;nculos con Ir&aacute;n. La maniobra fracas&oacute; cuando Jaafari logr&oacute; que diputados chi&iacute;tas respaldaran su permanencia  como primer ministro, gracias al apoyo de Moktada al-Sadr. <p>
Ahora la bomba de Samarra y sus repercusiones han puesto al descubierto lo que el prominente comentarista conservador William F. Buckley llama el "fracaso" de EEUU en la ocupaci&oacute;n de Irak.<p>

