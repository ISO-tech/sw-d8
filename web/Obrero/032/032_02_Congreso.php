<font face="Times New Roman, Times, serif" size="5"><b>&iquest;Por qu&eacute; el Congreso Dem&oacute;crata no puede?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">abril-mayo de 2007 | p&aacute;gina 2</font><p>

<font face="Times New Roman, Times, serif" size="3">DE LAS elecciones pasadas aprendimos que una clara mayor&iacute;a queremos ver la guerra contra Irak terminada. Pero despu&eacute;s de cuatro meses bajo control Dem&oacute;crata, el Congreso no ha sacado un voto que al menos meng&uuml;e la capacidad de guerra de la administraci&oacute;n Bush.<p>
En marzo, la C&aacute;mara de Representantes aprob&oacute; un paquete suplementario de gastos de guerra propuesto por los Dem&oacute;cratas que incluye m&aacute;s de $120 miles de millones para las guerra de Irak y Afganist&aacute;n, mucho m&aacute;s de lo Bush pidi&oacute;. Adem&aacute;s, aunque requiere que la mayor&iacute;a de las tropas de combate sean retiradas para el 31 del 2008, el inocuo documento tambi&eacute;n le permite al presidente ignorar estos requerimientos por razones de "seguridad nacional".<p>
Al proyecto le removieron el requisito de que Bush obtuviera aprobaci&oacute;n del Congreso antes de tomar cualquier acci&oacute;n militar contra Ir&aacute;n. Peor a&uacute;n, propuesta fue apoyada por muchos liberales como un paso hacia terminar la guerra.<p>
Las representantes Dem&oacute;cratas Barbara Lee, Maxine Waters y Lynne Woolsey, miembros del caucus Fuera de Irak, propusieron una resoluci&oacute;n m&aacute;s para poner una fecha de retiro de las tropas dentro de seis meses, la que recibi&oacute; mucho apoyo del sector anti guerra, pero en &uacute;ltima instancia, las tres congresistas abandonaron su resoluci&oacute;n a favor de la propuesta del liderato Dem&oacute;crata, para evitar que el Congreso implementara recortes severos al prepuesto b&eacute;lico.<p>
Frente a la pregunta de que si los Dem&oacute;cratas cortar&aacute;n los fondos de guerra o no, Tina Richards, activista anti-b&eacute;lica y madre de un soldado a punto de ser enviado por tercera vez a Irak, trat&oacute; de obtener una respuesta del representante David Obey, Dem&oacute;crata de Wisconsin, l&iacute;der del Comit&eacute; de Apropiaciones de la Casa de Representantes, que maneja entre otros los presupuestos de guerra.<p>
En un fortuito encuentro en los pasillos del Congreso, Richards pregunt&oacute; al representante por qu&eacute; los Dem&oacute;cratas no han actuado m&aacute;s decisivamente con respecto de la guerra. A lo que Obey le respondi&oacute; gritando "&iexcl;No tenemos los votos para desfinanciar la guerra". Luego, denunciando a "esos est&uacute;pidos liberales" que quieren cortar los fondos para la guerra, Obey dej&oacute; bien en claro que "&iexcl;no debi&eacute;ramos!"<p>
Las tr&eacute;mulas rodillas de los Dem&oacute;cratas parecen ceder r&aacute;pidamente ante la presi&oacute;n de los Republicanos, quienes les acusan de "no apoyar a nuestras tropas". Existen grupos liberales de cabildeo que se les unen a los Dem&oacute;cratas en este aspecto.<p>
"Lo que hemos hecho es una campa&ntilde;a para detener la guerra sin cortarle los fondos" a las tropas, dijo Tom Mazzie de Americanos contra el Escalamiento de la Guerra en Irak. Pero una "campa&ntilde;a para terminar la guerra sin cortar los fondos" es en realidad una campa&ntilde;a para NO acabar con la guerra. El grupo Move.on respald&oacute; la propuesta que fue aprobada por la c&aacute;mara baja.<p>
Los Dem&oacute;cratas est&aacute;n interesados en salvar la guerra de los desastrosos errores de la administraci&oacute;n Bush, no en terminarla. Pero la posibilidad de galvanizar apoyo para una inmediata retirada de Irak es clara.<p>
Una reciente petici&oacute;n comenzada por Noam Chomsky, Howard Zinn, y Cindy Sheehan entre otros, llama "a los EE.UU. a salirse de Irak, no en seis meses, no en un a&ntilde;o, sino que ahora". En Vermont , la legislatura estatal aprob&oacute; una resoluci&oacute;n llamando a las fuerzas estadounidenses a "retirarse inmediatamente" de Irak. Y las protestas en contra de la guerra de finales de enero y mediados de marzo muestran el potencial para sustentar la demanda de una retirada inmediata de las tropas de Irak, y para la construcci&oacute;n de un fuerte movimiento anti-b&eacute;lico en Estados Unidos.<p>

