<font face="Times New Roman, Times, serif" size="4">Paco Ignacio Taibo II: </font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"O los paramos o algo va a pasar"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">28 de enero de 2005</font><p>

<font face="Times New Roman, Times, serif" size="3">Entrevista exclusiva para <i>International Socialism Review</i> y <i>Obrero Socialista.</i>  <p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>&iquest;POR QU&Eacute; crees que es importante que la gente de Estados Unidos, particularmente su poblaci&oacute;n latina, sepan de los eventos de 1968 en M&eacute;xico?</b> <p>

SIEMPRE ES importante recuperar la memoria hist&oacute;rica, porque la memoria hist&oacute;rica construye los signos de identidad m&aacute;s potentes de los ciudadanos, de donde vinimos y por lo tanto quienes somos. Por lo tanto, venir a contar esta historia entre j&oacute;venes de origen mexicano siempre es importante. Contarla una y otra vez. &Eacute;sta es una historia que se ha convertido en una parte fija de la conciencia colectiva del pueblo mexicano. Y en este sentido, reemitir este trabajo me parece importante. Para los j&oacute;venes latinoamericanos, es un referente que no tienen. Y es curioso, tienes el referente Franc&eacute;s, sabes sobre el '68 en Italia, pero no sabes sobre el '68 mexicano que lo ten&iacute;as m&aacute;s cerca. Bueno, juega una funci&oacute;n informativa, pero aparte, juega una funci&oacute;n narrativa. Es una historia que tiene que ser contada.<p>

<b>&iquest;PIENSAS QUE <i>'68 </i>tiene importancia o que ofrece lecciones para los movimientos sociales de EE.UU., por ejemplo el movimiento latino o el movimiento antiguerra?</b> <p>

SI LO pienso. Pienso que el movimiento del '68 fue un movimiento de gran riqueza en cuanto a las formas y los planteamientos del desarrollo. Interesante en t&eacute;rminos de todo, desde su liderato, hasta como naci&oacute;, y la manera en que surgi&oacute;. Quisiera que el entendimiento de un movimiento social produzca y genere cultura informal, educaci&oacute;n sentimental, maldita sea. Tampoco pretendo que mi libro se torne en una especie de manual sobre el 68. M&aacute;s bien lo que es, una reflexi&oacute;n, narrativa personal, que te abre una puerta, una m&aacute;s de las muchas puertas que nos abren a lo largo del d&iacute;a. <p>

<b>&iquest;MIRANDO LA situaci&oacute;n pol&iacute;tica actual de M&eacute;xico, crees que es posible crear otro movimiento tan grande como el del 68, despu&eacute;s de lo que has llamado esta "transici&oacute;n descafeinada" de la dictadura del PRI?</b> <p>

HAY UNA fuerte baja en los movimientos sociales.  La acci&oacute;n pol&iacute;tica/electoral le ha robado espacio a los movimientos sociales, de alguna manera. Sin embargo, el grado de tensi&oacute;n en la sociedad mexicana sigue creciendo.  Los avances del proyecto neoliberal bajo el Foxismo han sido cada vez m&aacute;s virulentos y m&aacute;s agresivos. Entonces, es parad&oacute;jico: crece la tensi&oacute;n social, pero no se expresa socialmente. Pero es una paradoja temporal. Tarde o temprano, se expresar&aacute;.<p>

<b>MiRANDO LAS grandes manifestaciones de 500,000 personas en defensa del alcalde de Cuidad M&eacute;xico, L&oacute;pez Obrador, &iquest;crees que esta es una expresi&oacute;n de esta tensi&oacute;n social?</b> <p>

S&Iacute;, PERO indirecta. Es la manera como la lucha pol&iacute;tica/electoral ha robado el espacio de la lucha social de alguna manera. Pero tambi&eacute;n insisto es una coyuntura temporal marcada por los tiempos en que hemos vivido. La tensi&oacute;n social va a seguir creciendo cada vez m&aacute;s y ya planteado en t&eacute;rminos de "o los paramos o algo va a pasar". Es muy dif&iacute;cil saber: en M&eacute;xico las cosas explotan de manera muy extra&ntilde;a. Nadie nunca pens&oacute; de las Zapatistas antes de los Zapatistas. Nadie fue capaz de prever un movimiento de medio mill&oacute;n de estudiantes un mes antes del movimiento del 68. Nadie tuvo ninguna capacidad para hablar de la explosi&oacute;n del Cardenismo como fen&oacute;meno electoral, pero en aquel momento, m&aacute;s que electoral, era la ruptura masiva de millares de ciudadanos sobretodo con bases suburbanas y campesinas. M&eacute;xico, para los previsores, es un fracaso. Si quieres ser previsor, vete a Paraguay, no a M&eacute;xico. En el sentido de que la gente no est&aacute; contenta con como est&aacute;n las cosas. Porque la explosi&oacute;n social puede producirse. Porque la tensi&oacute;n ah&iacute; est&aacute;, es de niveles muy jodidos y muy profundos. El proyecto neoliberal ha sido verdaderamente un gran desastre.<p>

<b>ES UNA din&aacute;mica muy similar a la de Estados Unidos durante el &uacute;ltimo a&ntilde;o, en que todo el mundo se enfocaba en las elecciones entre Kerry y Bush, y no hemos visto muchas movilizaciones sociales.</b> <p>

YO CREO que ahora que hay un tiempo de descanso y de reposo, va a resurgir. Estados Unidos va a generar en los pr&oacute;ximos dos a&ntilde;os un gran movimiento contra la guerra. Es inevitable. Porque la l&oacute;gica demencial belicista de Bush y sus primos y aliados y amigos y negocios, sobre todo la l&oacute;gica de la corporaci&oacute;n financiero-militar que esta guerra sigue. En esa medida, los cad&aacute;veres siguen regresando a casa. La informaci&oacute;n que lentamente va permearndo la sociedad, va a crear una movilizaci&oacute;n contra la guerra.<p>

