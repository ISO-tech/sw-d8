<font face="Times New Roman, Times, serif" size="5"><b>Dictador y carnicero apoyado por EE.UU.</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">28 de enero de 2005 | p&aacute;gina 3</font><p>

<font face="Times New Roman, Times, serif" size="3">SI EL ex-dictador de Chile, Augusto Pinochet, es finalmente sometido a juicio por sus cr&iacute;menes, su protector, Henry Kissinger, antiguo Secretario de Estado de EE.UU., debiera comparecer junto a &eacute;l. <p>
En diciembre se le expidieron acusaciones a Pinochet, quien dirigi&oacute; el golpe militar que derroc&oacute; el gobierno democr&aacute;ticamente electo del Presidente chileno Salvador Allende en 1973, por lo que podr&iacute;a comparecer a juicio por parte de los aproximadamente 45,000 asesinatos pol&iacute;ticos cometidos bajo su gobierno. <p>
Kissinger, seg&uacute;n muestran documentos desclasificados por el gobierno de EE.UU., orquest&oacute; los esfuerzos para desestabilizar el gobierno de Allende, que estaba compuesto de una coalici&oacute;n de partidos de socialistas. "No veo porque tenemos que cruzarnos de brazos y permitir que un pa&iacute;s se convierta en comunista debido a la irresponsabilidad de su propio pueblo", dijo &eacute;l despu&eacute;s de que Allende fue electo.<p>
Los activistas en solidaridad con Latinoam&eacute;rica y tambi&eacute;n autores, Roger Burbach y Paul Cantor, describieron el impacto del golpe apoyado por EE.UU.: "La junta militar dirigida por Pinochet disolvi&oacute; el Congreso, proscribi&oacute; los partidos pol&iacute;ticos y el sindicato m&aacute;s grande del pa&iacute;s, censur&oacute; la prensa, prohibi&oacute; la pel&iacute;cula <i>El Violinista en el Tejado</i> como propaganda marxista, quem&oacute; libros p&uacute;blicamente...expuls&oacute; a estudiantes y profesores de las universidades, instal&oacute; a oficiales militares como rectores universitarios, y detuvo, tortur&oacute; y mat&oacute; a miles de opositores del r&eacute;gimen".<p>
Pinochet fue tambi&eacute;n responsable de la Operaci&oacute;n C&oacute;ndor--una ola de asesinatos de opositores de izquierda a trav&eacute;s de Sudam&eacute;rica aprobada por la CIA. Seg&uacute;n inform&oacute; el <i>New York Times</i> el mes pasado, la respuesta de Washington fue el darle a Pinochet "un pago de $3 millones debido a deberes oficiales que implican a los Estados Unidos". <p>
Pinochet se jubil&oacute; en 1990. La primera tentativa de procesarlo en 1998 se esfum&oacute; cuando el viejo carnicero fingi&oacute; estar muy enfermo como para enfrentar un juicio. Sin embargo, en meses recientes, Pinochet ha defendido p&uacute;blicamente su brutalidad--y la presi&oacute;n popular forz&oacute; a los procuradores a procesarlo una vez m&aacute;s.<p>
Si Pinochet se sent&iacute;a lo suficiente seguro como para jactarse, es porque se sinti&oacute; mas confiado debido a la pol&iacute;tica cada vez m&aacute;s agresiva de Washington en Latinoam&eacute;rica--que incluye la militarizaci&oacute;n de la regi&oacute;n de los Andes con el Plan Colombia, as&iacute; como el apoyo de EE.UU. al golpe fallido contra el presidente venezolano Hugo Ch&aacute;vez. <p>
Washington est&aacute; decidido a trazar una l&iacute;nea contra los movimientos sociales y las rebeliones populares que han expulsado los gobiernos de varios pa&iacute;ses latinoamericanos. As&iacute; en noviembre, el Secretario de Defensa Donald Rumsfeld utiliz&oacute; una reuni&oacute;n de ministros de defensa del Hemisferio Occidental para hacer un llamado para la coordinaci&oacute;n multinacional de fuerzas armadas en las Am&eacute;ricas para luchar contra el "terrorismo"--lo que significa el resurgimiento de las estructuras militares conjuntas utilizadas en la era de Pinochet, en sus "guerras sucias" contra la izquierda. <p>
Si Pinochet es sometido a juicio, debemos utilizar esta oportunidad no s&oacute;lo para exponer los cr&iacute;menes pasados patrocinados por EE.UU., pero tambi&eacute;n para organizarnos contra atrocidades presentes y futuras.<p>
<i>Traducci&oacute;n de H&eacute;ctor Reyes</i> <p>

