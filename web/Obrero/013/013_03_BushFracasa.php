<P><font face="Times New Roman, Times, serif" size="5"><b>Bush fracasa otra vez en su acoso de Am&eacute;rica Latina</b></font></P>

<P><font face="Times New Roman, Times, serif" size="2"><b>Por Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | 23 de enero de 2004 | p&aacute;gina 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON HA tomado la ofensiva en Am&eacute;rica Latina en todos los frentes--pol&iacute;tico, econ&oacute;mico y militar. Pero la resistencia popular contra la pol&iacute;tica de "libre comercio"--conocida como el neoliberalismo--todav&iacute;a esta en auge en Latinoam&eacute;rica.</P>
<P>El ejemplo m&aacute;s reciente: la cumbre de la Organizaci&oacute;n de Estados Americanos (OEA) en Monterrey, M&eacute;xico. La cumbre--una reuni&oacute;n de los l&iacute;deres de los 34 pa&iacute;ses de las Am&eacute;ricas, excluyendo a Cuba--fue dise&ntilde;ada para discutir los problemas de desarrollo econ&oacute;mico y social.</P>
<P>Pero George W. Bush lleg&oacute; con otra meta--impulsar las negociaciones por el libre comercio en las Am&eacute;ricas. Sin embargo, no tuvo &eacute;xito--por tercera vez en cinco meses.</P>
<P>En septiembre de 2003, las negociaciones de la Organizaci&oacute;n Mundial del Comercio (OMC), en M&eacute;xico tambi&eacute;n, fracasaron cuando EE.UU. y la Uni&oacute;n Europea se opusieron a eliminar sus propios subsidios agr&iacute;colas, pero exigieron completa libertad para las inversiones de las grandes empresas transnacionales en Am&eacute;rica Latina y otros pa&iacute;ses en desarollo. Brasil, junto a India y China, bloquearon las negociaciones.</P>
<P>Anteriormente en Miami, en la cumbre de la &Aacute;rea de Libre Comercio de las Am&eacute;ricas (ALCA), Brasil fue el actor principal en la campa&ntilde;a para forzar a Washington a retroceder y aceptar un "ALCA Liviano". All&iacute;, frente a una severa represi&oacute;n polic&iacute;aca, marcharon contra el ALCA unos 25,000 sindicalistas y activistas del movimiento por la justicia global. Asistieron l&iacute;deres importantes de Am&eacute;rica Latina a estas protestas, como el sindicalista boliviano Oscar Olivera y Blanca Chancoso, una l&iacute;der del movimiento ind&iacute;gena en Ecuador.</P>
<P>La oposici&oacute;n por parte de los gobernantes--principalmente por los presidentes Lu&iacute;s In&aacute;cio da Silva (Lula) de Brasil, N&eacute;stor Kirchner de Argentina y Hugo Ch&aacute;vez de Venezuela--fue un reflejo de la presi&oacute;n popular de los movimientos sociales y los sindicatos. Y la rebeli&oacute;n masiva que derroc&oacute; al ex-presidente Gonzalo S&aacute;nchez de Lozada de Bolivia en octubre de 2003 fue solamente el ejemplo m&aacute;s reciente del derrocamiento de l&iacute;deres suramericanos como resultado de huelgas y protestas enormes en los &uacute;ltimos a&ntilde;os--incluyendo a Per&uacute;, Argentina, y Ecuador (dos veces).</P>
<P>Lula y los otros gobernantes se oponen a la pol&iacute;tica de Washington no solamente por la presi&oacute;n desde abajo, sino tambi&eacute;n como forma de adelantar las aspiraciones de la clase capitalista de sus propios pa&iacute;ses. En el caso de Lula, el primer presidente del Partido de Trabajadores (PT), &eacute;l ha atacado las pensiones y ha mantenido las tasas de inter&eacute;s altas para que satisfacer a los banqueros. </P>
<P>La oposic&iacute;on de Lula al ALCA es condicional, pues &eacute;l es respaldado por las empresas exportadoras brasile&ntilde;as de aviones, acero y productos agr&iacute;colas. Estas empresas quieren un ALCA diferente--pero s&iacute; est&aacute;n de acuerdo con la necesidad de atacar los salarios y los beneficios de los obreros, los campesinos y los pobres.</P>
<P>Washington continuar&aacute; tratando de imponer su programa--sea usando la fuerza militar, como en Colombia, o firmando acuerdos de libre comercio. Es solo a trav&eacute;s de la lucha--que ahora va en aumento--que podremos detener al ALCA y al programa de Bush.</P>


