<P><font face="Times New Roman, Times, serif" size="4">Captura de Saddam no cambia nada</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Esto no es liberaci&oacute;n</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">23 de enero de 2004 | p&aacute;gina 4</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"LO CAPTURAMOS". El jefe colonial de Washington, Paul Bremer, no pudo ser m&aacute;s jaquet&oacute;n al anunciar que las fuerzas estadounidenses finalmente hab&iacute;an capturado el "as de espadas" d&iacute;as antes de la Navidad. George W. Bush, empapado de arrogancia, sonre&iacute;a presumidamente para sus fotos publicitarias, y los aduladores pro-guerra de los medios de comunicaci&oacute;n se regocijaban mientras transmit&iacute;an interminablemente las im&aacute;genes de Saddam Hussein siendo empujado y pinchado como un animal durante un supuesto examen f&iacute;sico.</P>
<P>El mensaje de los belicosos de Washington era claro--finalmente hab&iacute;an ganado una victoria al haber "agarrado a su presa". Pero pocas semanas despu&eacute;s, la verdad volvi&oacute; a asomar la cabeza por entre esta nueva cortina de humo de Bush--que la pesadilla de la ocupaci&oacute;n de Irak por Washington contin&uacute;a.</P>
<P>Por ejemplo, la captura de Saddam no alivia el dolor de los muchos iraqu&iacute;es que han sufrido tanta guerra, ni consuela a los soldados estadounidenses que han dejado sus familias para defender el poder colonial. Es un chiste cruel el tratar de decirles a los iraqu&iacute;es que viven bajo la bota del ejercito de EE.UU. que ahora s&iacute; est&aacute;n "liberados" despu&eacute;s de la captura de Saddam.</P>
<P>"Ustedes nos dijeron que nos traer&iacute;an libertad y democracia, pero &#191;qu&eacute; se supone que pensemos?" le pregunt&oacute; Hamed Hussein al periodista Robert Fisk del peri&oacute;dico brit&aacute;nico <I>The Independent</I>. "A mi vecino, los americanos se lo llevaron frente a su esposa y sus dos hijos, le amarraron las manos en la espalda, y pocas horas despu&eacute;s, luego de toda esta humillaci&oacute;n, regresaron y le dijeron a su esposa que cogiera todas sus pertenencias m&aacute;s caras, y pusieron explosivos en su casa y la volaron. El es un agricultor. Es inocente. &#191;Qu&eacute; hemos hecho para merecernos esto?"</P>
<P>Nada, eso es la respuesta. Irak hoy es una pesadilla sangrienta que no tiene nada que ver con la libertad o la justicia para el pueblo iraqu&iacute;. Han dejado de contar las v&iacute;ctimas iraqu&iacute;es de la ocupaci&oacute;n, pero sigue aumentando el n&uacute;mero de presos, desocupados y desamparados. </P>
<P>Y el n&uacute;mero de soldados estadounidenses muertos en el pa&iacute;s sobrepas&oacute; 500 en enero. Esto no anima a los miles de reservistas de EE.UU., a quienes les prometieron que los iraqu&iacute;es los recibir&iacute;an como "libertadores", pero que ahora enfrentan una resistencia m&aacute;s hostil a su presencia. En la primavera ir&aacute;n otras tropas&#151;40% de ellos reservistas--a reemplazar los soldados que llevan m&aacute;s de un a&ntilde;o all&iacute;.</P>
<P>La captura de Saddam Hussein tampoco ha impedido las bajas sufridas por las tropas tailandesas y b&uacute;lgaras, que murieron por bombas ocultas en autom&oacute;viles en Karbala, ciudad del sur de Irak.</P>
<P>La guerra no ten&iacute;a nada que ver con acabar con la tiran&iacute;a de un dictador que antes era respaldado entusiasmadamente por EE.UU. Para reemplazar el poder colonial, Bremer y Bush s&oacute;lo proponen una legislatura "interina" escogida por participantes "moderados" iraqu&iacute;es favorecidos por la coalici&oacute;n estadounidense. Ahora lo m&aacute;s alarmante para Bremer es que los shi&iacute;tas, bajo el liderazgo del Gran Ayatolah Ali al-Sistani, est&aacute;n reclamando su derecho a elecciones directas para el pueblo iraqu&iacute;. </P>
<P>La guerra y la ocupaci&oacute;n de George Bush siempre han sido para garantizar las ganancias petroleras de las corporaciones estadounidenses y para expandir el imperio pol&iacute;tico y econ&oacute;mico de Washington. Es por esto que la resistencia al dominio de EE.UU. sobre Irak continuar&aacute; creciendo, con m&aacute;s ataques iraqu&iacute;es contra la autoridad "provisional" como la bomba suicida contra la sede estadounidense de ocupaci&oacute;n en enero.</P>
<P>Es hora de exigir: &#161;Alto a la ocupaci&oacute;n ahora!</P>


