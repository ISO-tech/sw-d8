<P><font face="Arial, Helvetica, sans-serif" size="2"><b>LO QUE PENSAMOS</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Ocupaci&oacute;n no es liberaci&oacute;n</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>&#161;Que regresen las tropas ahora!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">27 de mayo de 2004 | p&aacute;gina 2</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>LAS FOTOS de los ata&uacute;des de soldados estadounidenses muertos en Irak finalmente aparecieron en los medios noticiosos nacionales--y a la administraci&oacute;n de Bush le dio una rabieta. Esto no es sorprendente. Las filas ordenadas de ata&uacute;des cubiertos con banderas son prueba visual del costo cada vez m&aacute;s alto de la guerra de Bush.</P>
<P>La Casa Blanca estaba tan determinada a mantener estas im&aacute;genes ocultas que prohibi&oacute; a los medios la entrada a la base de la fuerza a&eacute;rea en Dover, donde se descargan los ata&uacute;des. "La sensibilidad y la privacidad de las familias de los ca&iacute;dos deben ser la primera prioridad", dijo el portavoz de Bush, Trent Duffy.</P>
<P>&#161;Qu&eacute; hipocres&iacute;a! Bush no ha expresado su supuesto dolor ni en un solo entierro de ning&uacute;n soldado muerto en Irak. Y tampoco se preocup&oacute; por la "sensibilidad" de &eacute;stas familias cuando envi&oacute; a sus hijos a luchar en una guerra predicada sobre mentiras y enga&ntilde;o.</P>
<P>Si la administraci&oacute;n de Bush se sale con la suya, el n&uacute;mero de muertos--iraqu&iacute;es y estadounidenses--solamente continuar&aacute; creciendo. El vicepresidente Dick Cheney subray&oacute; la determinaci&oacute;n de la administraci&oacute;n de Bush de aplastar a sus adversarios en Irak--no importa el costo humano--en t&eacute;rminos escalofriantes. "Con este tipo de enemigo no se puede negociar, no lo podemos disuadir, contener, o apaciguar", declar&oacute; Cheney. "Solamente puede ser destruido. &Eacute;sta es nuestra tarea inmediata".</P>
<P>Esta tarea la apoya todo Washington--no solamente los fan&aacute;ticos "halcones" imperialistas en la administraci&oacute;n de Bush. Los senadores Chuck Hagel (Republicano de Nebraska) y Joseph Biden (Dem&oacute;crata de Delaware), ambos miembros del Comit&eacute; de Relaciones Exteriores, flotaron la propuesta de reestablecer el servicio militar obligatorio para asegurar niveles adecuados de tropas--aunque trataron de enmascarar su llamado disfraz&aacute;ndolo de preocupaciones sobre que los pobres y la juventud de la clase obrera no carguen exclusivamente con el peso de luchar la guerra.</P>
<P>El general John Abizaid, comandante m&aacute;ximo de EEUU en el Medio Oriente, comenz&oacute; a insinuar que solicitar&aacute; otra extensi&oacute;n del servicio activo militar para mantener el nivel de tropas actual en 135,000--y con la posibilidad de que pida a&uacute;n m&aacute;s soldados m&aacute;s all&aacute; de esto.</P>
<P>El candidato presidencial Dem&oacute;crata John Kerry est&aacute; de acuerdo con &eacute;l. Propone enviar m&aacute;s tropas a Irak que George Bush. En vez de oponerse a la ocupaci&oacute;n de Bush en pos de petr&oacute;leo e imperio, Kerry se presenta como alguien que ofrece una mejor manera de pelear la guerra--mientras que el n&uacute;mero de muertos sigue creciendo.</P>
<P>El gobierno estadounidense no tiene ning&uacute;n derecho a determinar el futuro de Irak. Sin embargo, los funcionarios de EE.UU. est&aacute;n cuidadosamente escogiendo un gobierno provisional para una "transferencia de poder" el 30 de junio que en realidad dejar&aacute; a Washington con el control militar completo de Irak.</P>
<P>La ocupaci&oacute;n por EE.UU.--no importa si su poder se manifiesta directamente con soldados norteamericanos, o entre bastidores a trav&eacute;s de un gobierno de t&iacute;teres--solamente crear&aacute; una resistencia mayor hasta que Washington sea expulsado. Tenemos que apoyar la lucha de los iraqu&iacute;es por su liberaci&oacute;n--&#161;y exigir que EEUU saque sus tropas de Irak ahora! </P>

