<P><font face="Times New Roman, Times, serif" size="5"><b>Expuestos a la radiaci&oacute;n en Irak</b></font></P>

<P><font face="Times New Roman, Times, serif" size="2"><b>Por H&eacute;ctor Reyes</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | 27 de mayo de 2004 | p&aacute;gina 4</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>Una docena de soldados puertorrique&ntilde;os han sido evacuados de Irak desde abril mostrando s&iacute;ntomas compatibles con la exposici&oacute;n a la radioactividad--falta de sue&ntilde;o, dolor de cabeza y sangre en la orina. Seg&uacute;n el servicio de noticias Associated Press, en abril "cuatro soldados que participaron en la guerra dieron positivo en pruebas que indicaban altos niveles de contaminaci&oacute;n con uranio reducido".</P>
<P>El uranio reducido (UR) es usado por el aparato militar para revestir proyectiles y aumentar su capacidad para penetrar el blindaje de veh&iacute;culos enemigos. Miles de bombas revestidas con UR fueron usadas durante la guerra en Irak. Al impactar su blanco, los proyectiles con UR no solo contaminan su blanco, sino tambi&eacute;n el &aacute;rea circundante al producir part&iacute;culas de UR, convirti&eacute;ndose en polvo radioactivo.</P>
<P>A pesar de que el gobierno de EE.UU. se rehusa a admitirlo, existe bastante evidencia que confirma los peligros del UR. Irak se encuentra repleto de lugares contaminados con UR. La poblaci&oacute;n de Irak--con exorbitantes tasas de c&aacute;ncer--se encuentra obscenamente afectada por este otro abuso del imperialismo estadounidense.</P>
<P>Las tropas estadounidenses tampoco son orientadas sobre este peligro. Se considera que el UR es uno de los contribuyentes al llamado S&iacute;ndrome del Golfo que ha afectado a m&aacute;s de 200 mil soldados estadounidenses veteranos de la guerra contra Irak en 1991. Luego de que sali&oacute; a la luz p&uacute;blica que estos soldados hab&iacute;an dado positivo a la contaminaci&oacute;n con UR, sobre mil soldados corrieron a hacerse pruebas al hospital militar Walter Reed en Washington, D.C. Y aunque supuestamente sus pruebas salieron negativas, se reconoce que "los m&eacute;todos instrumentales usados en Estados Unidos para detectar [el UR] no son muy exactos", de acuerdo al peri&oacute;dico <I>Army Times.</P>
</I><P>Aunque los soldados boricuas contaminados con UR son residentes en Nueva York, las revelaciones causaron gran malestar en la isla, ya que el pueblo entiende que la Marina de EE.UU. utiliz&oacute; bombas revestidas con UR en su campo de tiro en Vieques. Muchos indican que esto tiene que ver con que la poblaci&oacute;n viequense tenga la tasa de c&aacute;ncer m&aacute;s alta de Puerto Rico. Sobre 5,000 soldados puertorrique&ntilde;os provenientes de la isla han sido movilizados durante el actual conflicto. Sobre una docena ya han muerto.</P>
<P>La alta tasa de bajas latinas es otra muestra del racismo del gobierno estadounidense que usa a los pobres y a las minor&iacute;as raciales como carne de ca&ntilde;&oacute;n para una guerra basada en mentiras y estereotipos racistas.</P>

