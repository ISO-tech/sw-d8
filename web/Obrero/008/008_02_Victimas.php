<P><font face="Times New Roman, Times, serif" size="4">Cacer&iacute;a de brujas del gobierno federal contra &aacute;rabes y musulmanes</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>V&iacute;ctimas de la guerra dom&eacute;stica</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">14 de febrero de 2003 | P&aacute;gina 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>NICOLE COLSON demuestra c&oacute;mo los ataques de Ashcroft est&aacute;n destruyendo las vidas de los inmigrantes. </b></P>
<P>JOHN ASHCROFT dice que la nueva regla har&iacute;a a EE.UU. "m&aacute;s seguro", mientras que "protege los derechos de individuos que vienen aqu&iacute;". Debiera preguntarle a Bijan Pirazdeh si &eacute;l se siente "seguro" o "protegido".</P>
<P>Ashcroft hablaba acerca del Sistema Nacional de Seguridad de Inscripci&oacute;n de Salidas-Entradas, implementado despu&eacute;s del 11 de septiembre, que requiere que hombres mayores de 16 a&ntilde;os con visas temporarias de varios pa&iacute;ses se registren con el Servicio de Inmigraci&oacute;n y Naturalizaci&oacute;n (INS, por sus siglas en ingl&eacute;s). </P>
<P>El 16 de diciembre era la fecha l&iacute;mite para aquellos provenientes de Ir&aacute;n, Irak, Siria, Sud&aacute;n y Libia. Por esto Pirazdeh se present&oacute; a la sede del INS de Los Angeles a las 5 a.m., pensando llenar algunos papeles y entonces dirigirse a su trabajo como ingeniero de tr&aacute;nsito. Pero nunca lleg&oacute; a su trabajo ese d&iacute;a. Env&eacute;s fue encarcelado, y ahora enfrenta la deportaci&oacute;n.</P>
<P>Pirazdeh no est&aacute; solo. Tanto como unas 700 personas, la mayor&iacute;a de California, fueron detenidas como consecuencia de la pol&iacute;tica de inscripci&oacute;n. Esto porque algunas oficinas del INS decidieron detener a cualquiera con una "irregularidad" de inmigraci&oacute;n--aunque la ley actual de inmigraci&oacute;n da a los inmigrantes cuyas visas han expirado el derecho de regularizar su status tras pagar una multa y comparecer a una audiencia si &eacute;stos tienen lazos familiares en EE.UU., un trabajo y un record limpio.</P>
<P>Pero no esperemos ninguna simpat&iacute;a por parte del gobierno federal. Cuando miles de manifestantes enojados se reunieron fuera del edificio de la INS en Los Angeles luego de los arrestos, el oficial de relaciones con la comunidad, Jorge Swank, tuvo la desfachatez de decirle a la muchedumbre: "Nosotros sentimos su dolor".</P>
<P>No puede haber dicho esto en serio. C&oacute;mo podr&iacute;a Swank sentir el dolor de Shahin Hajizadeh, que describi&oacute; el ser golpeado por la polic&iacute;a--o los muchos otros que contaron ser api&ntilde;ados en celdas, forzados a descansar parados y con un fr&iacute;o de nevera. </P>
<P>Swank no sabe nada del dolor de Gisroo Mohajeri, con  seis y medio meses de embarazo, cuyo hijo de 16 a&ntilde;o de edad, Hossein Ahmadi, fue detenido despu&eacute;s de irse a inscribir a insistencia de ella. Y si John Ashcroft se sale con la suya, las detenciones y los arrestos continuar&aacute;n acumul&aacute;ndose.</P>
<P>A pesar de la ola de protestas, el INS mantuvo su fecha l&iacute;mite de inscripci&oacute;n del 10 de enero para hombres de los Emiratos Arabes Unidos, Corea del Norte, Marruecos, Afganist&aacute;n y nueve otros pa&iacute;ses. Tiene por lo menos dos rondas m&aacute;s de inscripciones planificadas para m&aacute;s adelante. </P>
<P>Gente como Abdo, de Yemen, se preguntan si ellos tambi&eacute;n ser&aacute;n atrapados en la cacer&iacute;a de brujas. Abdo, quien es un conductor de microbuses del aeropuerto de Virginia, fue con su abogado a inscribirse a las oficinas del INS en Arlington.</P>
<P>Ah&iacute; le exigieron que subiera al cuarto piso, donde se encuentra la oficina de deportaci&oacute;n y detenci&oacute;n. Abdo tuvo suerte, y no fue arrestado. Pero centenares m&aacute;s no ser&aacute;n tan afortunados. Por eso los activistas est&aacute;n organizando manifestaciones en las sedes del INS en Nueva York, San Francisco, Chicago y otras ciudades para confrontar cada nueva fecha l&iacute;mite de inscripci&oacute;n con una oposici&oacute;n visible. </P>
<P>Seg&uacute;n Hussam Ayloush, director ejecutivo del Concilio de Relaciones Isl&aacute;micas con Am&eacute;rica en el sur de California, dijo en una manifestaci&oacute;n: "Este nuevo Macartismo contra los musulmanes debe acabarse. &#191;Nos van a deportar a todos?" </P>

