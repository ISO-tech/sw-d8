<font face="Times New Roman, Times, serif" size="4">Protestas espantan a racistas anti-inmigrantes</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Cazamigrantes derrotados</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Por Jocelyn Blake</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | septiembre-octubre de 2005</font><p>

<font face="Times New Roman, Times, serif" size="3">CIENTOS DE personas se arremolinaron a ambos lados de la verja que separa a Calexico, California, de Mexicali, M&eacute;xico, el 17 de septiembre para demostrar su oposici&oacute;n a los cazamigrantes. <p>
Los manifestantes golpearon la verja y gritaron "Queremos un mundo sin fronteras". Trepado en el tope de la muralla fronteriza, un manifestante levantaba una efigie de un cazamigrante vestido con la capucha blanca usada por el Ku Klux Klan.<p>
Alrededor de 300 de manifestantes llegaron a Calexico, en el lado estadounidense de la frontera, para demostrarle a los <i>Minutemen</i> y sus emuladores que no son bienvenidos en California.<p>
Un grupo que se llama Amigos de la Patrulla Fronteriza hab&iacute;a seleccionado el 16 de septiembre para movilizar una "patrulla c&iacute;vica" en la frontera cerca de Calexico, imitando el modelo anti-inmigrante de los <i>Minutemen</i> que invadieron a Arizona el pasado abril. En contraste, el fin de semana transcurri&oacute; sin evidencia de que alg&uacute;n cazamigrantes se hubiera presentado. S&oacute;lo se ve&iacute;an pancartas con consignas que dec&iacute;an "Minutemen, se les acab&oacute; el tiempo. V&aacute;yanse a casa", en las &aacute;reas donde los racistas hab&iacute;an planeado reunirse.<p>
Semanas antes de la protesta, el consejo municipal de Calexico aprob&oacute; un&aacute;nimemente una resoluci&oacute;n que denunciaba la presencia de cualquier grupo de cazamigrantes. "Ellos no son necesarios aqu&iacute;," dijo Alex Perrone, alcalde de Calexico. "Si vienen, les dir&eacute; a los Minutemen y los Amigos de la Patrulla Fronteriza, 'Que se vayan para Nueva Orle&aacute;ns. Muestren que son patriotas y ayuden a su pr&oacute;jimo'".<p>
Andy Ramirez, organizador de los Amigos de la Patrulla Fronteriza, le dijo al peri&oacute;dico <i>San Diego Union Tribune</i> que de las 125 de personas que hab&iacute;an sido entrenadas para la patrulla, s&oacute;lo iban a participar de 20 a 30--mucho menos de las 2,000 que el grupo hab&iacute;a anunciado originalmente.<p>
"Hemos reducido nuestros planes en parte por la candela que hemos recibido a ra&iacute;z de la fecha que escogimos", dijo Ramirez, refiri&eacute;ndose a la decisi&oacute;n descaradamente racista de comenzar su operaci&oacute;n el d&iacute;a de la independencia mexicana. Pero aun la pat&eacute;tica cifra de 20 a 30 personas termin&oacute; siendo una exageraci&oacute;n.<p>
Esta protesta se constituy&oacute; en otra victoria para los activistas pro inmigrantes en California, que han enfrentado a los cazamigrantes, tanto en la frontera como en movilizaciones en contra de los piquetes anti-inmigrantes en el &aacute;rea de Los &Aacute;ngeles.<p>
"Estas pol&iacute;ticas anti-terroristas, en que cualquier inmigrante es considerado un potencial terrorista, est&aacute;n alimentando el odio contra los inmigrantes", le dijo un residente local a <i>Obrero Socialista</i> durante la protesta. "No termina con los inmigrantes, y va a difundirse como odio en contra de otras gentes si no decimos "&iexcl;Abajo con este movimiento fascista!"<p>

