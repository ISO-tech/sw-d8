<font face="Times New Roman, Times, serif" size="5"><b>Los Dem&oacute;cratas y la Corte Suprema</b></font> <p>
<font face="Arial, Helvetica, sans-serif" size="2">julio-agosto de 2005 | p&aacute;gina 2</font><p>

<font face="Times New Roman, Times, serif" size="3">AL CIERRE de esta edici&oacute;n, el presidente Bush estaba preparando su anuncio del nuevo candidato a la Corte Suprema.<p>
Los ultra conservadores abogaban por un juez que conformara a su campa&ntilde;a a restringir los derechos al aborto y de los gays. Ni siquiera el Procurador General Alberto Gonz&aacute;lez, quien favorec&iacute;a la tortura de presos en Guant&aacute;namo, les parece suficientemente conservador.<p>
Mientras tanto, los grupos liberales como la Organizaci&oacute;n Nacional de Mujeres (NOW por sus siglas en ingl&eacute;s) cuentan con el partido dem&oacute;crata para proteger el derecho al aborto y los contraceptivos. Pero no podemos contar con los dem&oacute;cratas para defender los derechos. <p>
Se dejaron vencer con los nombramientos de los jueces ultraconservadores a las cortes federales. Ahora muestran esta misma tendencia hacia "la concertaci&oacute;n" pol&iacute;tica al aprobar un candidato para reemplazar a Sandra Day O'Connor. Elogian a &eacute;sta como "protectora moderada" de los derechos de la mujer, pero aunque O'Connor vot&oacute; a favor del derecho al aborto, tambi&eacute;n vot&oacute; por reducir el acceso para las mujeres pobres y las j&oacute;venes.<p>
Hay que recordar c&oacute;mo se gan&oacute; el derecho al aborto en 1973--no por el apoyo de los pol&iacute;ticos dem&oacute;cratas o la bondad de los jueces de la Corte Suprema. Hubo miles de mujeres, inspiradas por las otras luchas por los derechos civiles y contra la guerra, que manifestaron en las calles su deseo de controlar su propio cuerpo, y por lo tanto, su propio destino. <p>
Otra vez nos vemos en una coyuntura cr&iacute;tica en que hay que defender los derechos luchando en la calle--no detr&aacute;s de los dem&oacute;cratas. <p>

