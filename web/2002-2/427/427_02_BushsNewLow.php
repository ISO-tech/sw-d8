<P><font face="Times New Roman, Times, serif" size="5"><b>Bush gang stoops to brand-new low</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 25, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH gang has no shame. They're even trying to link the recent sniper attacks around the Washington, D.C., area to the "war on terrorism."</P>
<P>According to news reports, investigators will question detainees at the Guant&aacute;namo Bay, Cuba, prison camp--where prisoners from the U.S. war on Afghanistan were shipped last year and have been held ever since--about whether they have any information about the sniper.</P>
<P>U.S. officials say they plan to hold the prisoners indefinitely, subjecting them to periodic interrogations. This is despite the fact that there's no evidence at all that the attacks are linked to any terrorist groups.</P>
<P>In fact, until a witness to one killing purposely misled investigators with a description of a "Middle Eastern-looking man," even the 24-hour-a-day media talk show crowd assumed that the sniper was a native-born gun nut.</P>
<P>But the Bush administration couldn't pass up another opportunity--however cynical--to whip up hysteria against Arabs and build support for Bush's war.</P>

