<P><font face="Times New Roman, Times, serif" size="4">Say no to Bush's war on Iraq</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Speaking out in "too loud a voice"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 25, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ACTIVISTS ACROSS the country are organizing to make the October 26 antiwar demonstrations in Washington, D.C., and San Francisco as large as possible. And in the process, they're convincing more people to get involved in local antiwar groups at their schools and in their neighborhoods.</P>
<P>Last week, hundreds of students attended teach-ins at several campuses, including New York University (NYU), Columbia and Yale. At San Francisco State, antiwar activists successfully kicked off their first meeting, after the school administration harassed activists the week before. Officials sent armed police to inspect tabling permits, ordered staff to rip down antiwar posters and threatened to arrest an activist who "spoke in a too loud a voice." The group will have a contingent at the October 26 march in San Francisco.</P>
<P>The Berkeley Stop the War Coalition has organized a "Top Ten Reasons to Oppose the War on Iraq" postering campaign to advertise for the protest. At the University of Illinois-Chicago, 200 people attended an antiwar speak-out, including an active duty sergeant in the National Guard who took the mike to oppose the war.</P>
<P>NYU antiwar activists challenged the Republican Club to a debate, after the conservatives put up pictures of Iraqi children and the words: "Save the Children--Bomb Iraq." At the University of Texas-Austin, 3,000 people turned out to hear author Noam Chomsky speak against the war.</P>
<P>And local protests of George W. and his friends haven't let up. In Atlanta, hundreds of protesters confronted Dubya October 17 as he came to town to support candidate Saxby Chambliss. In Chicago, 200 people protested British Foreign Minister Jack Straw, with eight disrupting his speech with chants of "We don't want your war for oil!"</P>
<I><P>Candice Amich, Sam Bernstein, Todd Chretien, Mike Corwin, Desmond Gardfrey, Sarah Grey and Kirstin Roberts contributed to this report.</P>
