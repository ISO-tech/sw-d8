<P><font face="Times New Roman, Times, serif" size="5"><b>How the ILWU fought for Mumia</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 25, 2002 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>I recently read Todd Chretien and Sue Sandlin's article about the International Longshore and Warehouse Union (ILWU) lockout (<I>SW,</I> October 4). Being from Philadelphia, "Ground Zero" of the Mumia Abu-Jamal case, I cannot read about the Bush League's animosity towards the ILWU without remembering that this union shut down the West Coast ports in support of Mumia.</P>
<P>Bush's pick for "Homeland Security" czar is Tom Ridge, the former executioner governor of Pennsylvania. Ridge is part of the "kill Mumia" gang and is also the principle cause behind Philadelphia's Temple University permanently banning Pacifica Radio broadcasts--because Pacifica dared to broadcast Mumia's commentaries.</P>
<P>It's hard to imagine that the ILWU is not at the top of this administration's hit list--and that any labor-management incident won't be used to enact retribution.</P>
<B><P>John Jonik,</B> Philadelphia</P>

