<P><font face="Times New Roman, Times, serif" size="5"><b>Campaign to End the Death Penalty</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 25, 2002 | Page 14</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--Anti-death penalty activists, former death row prisoners and religious leaders held a press conference and rally last week to demand commutations for all Illinois death row prisoners. </P>
<P>Illinois Gov. George Ryan announced several weeks ago that he was considering a blanket commutation for those on death row in Illinois and that he would hold clemency hearings to help him make his decision.</P>
<P>About 100 people and 10 news stations attended the press conference, which came at a crucial time. The event gave a voice to anti-death penalty forces during a week in which the media gave daily coverage to advocates of the death penalty who cynically exploited the family members of murder victims to deflect attention from Illinois' broken death penalty system.</P>
<P>Among those who spoke were former Illinois death row prisoner Darby Tillis; Rev. Jesse Jackson Sr.; Alice Kim of the Campaign to End the Death Penalty; David Bates, a victim of police torture who spent years in prison on the basis of a confession tortured out of him by police; and Jennifer Bishop of Murder Victims' Families for Reconciliation.</P>
<P>But the highlights of the event were the speeches of Larry Ollins and Omar Saunders, two of the Roscetti Four--four young African American men who spent more than a decade in prison for the brutal 1986 rape and murder of Lori Roscetti before DNA evidence proved their innocence.</P>
<P>"We'd be dead right now if we were given the death penalty," Larry Ollins told <I>Socialist Worker</I>. "The privileges and the immunities of the Constitution should be distributed fairly and equally among everybody, but the proof is in the pudding, and in Cook County, in Chicago, we need some help over here. It's just like in the state of Virginia where you've got a madman running around with a gun. We got madmen running around in suits and ties with the power and the badge to go off into a neighborhood and wipe out a portion of the community by alleging they committed crimes they didn't do."</P>
<P>It's still not clear whether Ryan will commute some, all or none of the death sentences faced by 160 people on Illinois death row. This is the critical time to keep up the pressure.</P>

