<P><font face="Times New Roman, Times, serif" size="4">Eleven people found suffocated to death in railroad car</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Victims of Washington's war on immigrants</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 25, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE HORRIFIC consequences of U.S. immigration policy became all too clear in Denison, Iowa, last week.</P>
<P>Workers at a grain elevator discovered the bodies of 11 people who had been trapped in a grain car for at least four months. The four women and seven men were most likely undocumented Mexican immigrants who boarded the train after being smuggled into the U.S. The rail car left Matamoros, Mexico, in June and was stored in Oklahoma for months before being shipped to Iowa. </P>
<P>The circumstances of the deaths are horrifying.The rail car was latched from the outside, trapping the people inside. With no food or water--and temperatures that probably reached more than 130 degrees inside the steel car--doctors say that the victims may have become delirious and suffered hallucinations before finally slowly suffocating to death.</P>
<P>U.S. immigration policy is directly to blame for this nightmare. Every year, tens of thousands of undocumented immigrants come to the U.S., trying to escape poverty and hoping for a better life. Often, they must risk their own lives to escape being picked up by Border Patrol agents.</P>
<P>Last year, three people were found dead on train cars, and in 1999, 17 died in similar circumstances. Last June, 26 people were trapped inside two grain hopper cars in Combes, Texas, for hours--until they were discovered by Border Patrol dogs.</P>
<P>Since the Clinton administration launched the Operation Gatekeeper crackdown on immigrants in 1994--complete with the construction of a steel wall and more weapons for Border Patrol agents along the California-Mexico border--at least 700 people have died trying to cross into California. Adding in the border areas along Texas, New Mexico and Arizona, the death toll rises to more than 1,500. Meanwhile, undocumented workers who survive the crossing often have nothing better to look forward to than a miserable low-wage job.</P>
<P>The tragedy of these newest deaths is that they were totally preventable. They were caused by vicious immigration policies that label some human beings "illegal." As Rev. Jose Herrera told parishioners at a memorial service for the victims in Denison: "In olden times, it was said that we had to do what was said because it was the law. Today, we have to pay attention to the justice of the laws."</P>

