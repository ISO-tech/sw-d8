<P><font face="Times New Roman, Times, serif" size="4"><b>Florida's governor: Jeb Bigot</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH brothers can't seem to get enough of reveling in other people's pain and misery. First, George W. was caught in a televised interview mocking Karla Faye Tucker--a woman on Texas' death row whose execution he presided over as governor. Now, Dubya's baby brother, Florida Gov. Jeb Bush, is getting into the act.</P>
<P>At a recent meeting with Pensacola lawmakers, Jeb told the crowd that he had "devious plans" to prevent a constitutional amendment which would mandate smaller class sizes. He also said that he wants to "give the legislature the power to set teachers' salaries, bypassing locally elected school boards"--a reversal of the current Republican position.</P>
<P>But he got really vicious when he began to comment on the case of Rilya Wilson, the 6-year-old girl who has been "lost" without a trace in the state's foster care system for more than eight months.</P>
<P>To take the heat off the state's mismanagement, Pamela Graham and Geralyn Smith--Rilya's caregivers--were recently arrested on allegations that they illegally collected public assistance checks.</P>
<P>At the meeting with lawmakers, all Jeb could focus on was frat-boy gossip about the two women--implying that they are lesbians. "Bet you don't get that in Pensacola," Bush quipped.</P>
<P>--Associated Press, October 4, 2002</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Barbie's dream house gets invaded</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MOVE OVER, G.I. Joe. This year, for just under $45, parents can purchase "Forward Command Post"--a bombed-out version of Barbie's "dream house."</P>
<P>The small-scale, but realistic-looking two-story house has pale yellow walls, checkerboard floors and wood-framed windows. But the glass panes in the windows are cracked, and machine gun fire has sprayed the walls. </P>
<P>On the balcony stands a soldier in battle fatigues clutching an assault rifle. There's also a U.S. flag--to show that the neighborhood has officially been taken over.</P>
<P>If that wasn't bad enough, parents can buy their kids an "Osama bin Laden head," offered by Protect and Serve Toys, "to allow enthusiasts to enact what it may be like when we finally catch him."</P>
<P>--<I>San Francisco Chronicle,</I> October 4, 2002</P>

