<P><font face="Times New Roman, Times, serif" size="5"><b>Film marks 30 years after Bloody Sunday</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Shaun Harkin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 25, 2002 | Page 13</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>MOVIES: <I>Bloody Sunday,</B></I> written and directed by Paul Greengrass, starring James Nesbitt, Tim Pigott-Smith and Nicholas Farell.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>On January 30, 1972, British soldiers killed 13 unarmed marchers at a civil rights protest in Derry City, Northern Ireland--a slaughter that became known as "Bloody Sunday." And ever since, Britain has denied any wrongdoing.</P>
<P>Paul Greengrass made the film <I>Bloody Sunday</I> to mark the 30th anniversary of this outrage. Its documentary style, use of hand-held camera and some excellent acting make <I>Bloody Sunday</I> cinematically stunning. But the real source of the film's drama is what it reveals about Northern Irish politics.</P>
<P>The British government created Northern Ireland to maintain its control of part of Ireland, its first colony. All power rested in the hands of the pro-British Unionist government in Belfast. Discrimination against the Catholic minority was built into every state institution: voting rights, employment, housing, education, health care and the legal system.</P>
<P>British control was based on their support for the Protestant elite, who in turn depended upon sectarianism--notions of Protestant supremacy--to tie working-class Protestants to Britain and the Northern Irish bosses and keep them divided from working-class Catholics.</P>

