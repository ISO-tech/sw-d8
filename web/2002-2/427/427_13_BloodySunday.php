<P>In the late 1960s, Catholics in Northern Ireland--inspired by the Black civil rights movement in the U.S.--took to the streets to demand equality. The pro-British Unionist government responded with brutal repression, further radicalizing civil rights marchers.</P>
<P>The British government, fearing that the whole sectarian state was threatened, sent British soldiers in to "keep the peace" and "protect Catholics" from Unionist thugs. But it didn't take long for people to see through this pretext. Now, the British government was intervening "directly" to protect its interests and contain the Catholic rebellion.</P>
<P>Central to their strategy in the early '70s was the introduction of internment to drive working-class Catholics off the streets and end their mass protests. Internment terrorized whole communities. Thousands of innocent civilians were arrested and tortured, but protests against it where banned.</P>
<P>Toward the end of 1971, sections of the civil rights movement resolved to defy a ban on anti-internment demonstrations. This is where <I>Bloody Sunday</I> starts.</P>
<P>The camera follows Ivan Cooper--a Protestant member of Parliament representing Catholics and moderate civil rights leaders--on the day of the march. When he's warned that the march shouldn't go ahead because of the huge presence of British soldiers, Cooper argues that the civil rights movement will be "dead" if the march doesn't go ahead. What he means is that it will destroy the idea that Northern Ireland can be changed through peaceful means.</P>
<P>For 30 years, Britain has maintained it shot "terrorists" that day. <I>Bloody Sunday</I> depicts a very different story--that the British government planned to murder protesters in order to teach them a lesson. In fact, the British commander who organized the killings was later rewarded with a knighthood.</P>
<P>Overnight, Bloody Sunday radicalized a whole generation of Catholic youth. Thousands decided that British-dominated Northern Ireland couldn't be reformed and, lacking a credible revolutionary socialist alternative, joined the Irish Republican Army with the goal of ending British control.</P>
<P>Today, George Bush and Tony Blair are coming out with all sorts of justifications for occupying countries around the world. <I>Bloody Sunday</I> helps explain why we should reject this by showing who are the real "terrorists" at the source of the conflict.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>They can't hide the truth</b></font></P>
<P>JOE FRIEL was 20 years old when he was shot in the chest at the Bloody Sunday march. British soldiers claimed that Friel was armed--which proved to be a complete lie. Since then, Friel has campaigned for justice for the victims of Bloody Sunday. <I>Socialist Worker</I> spoke with Friel.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<B><P>A BRITISH soldier recently admitted that he lied about what transpired at the Bloody Sunday march. What's your reaction?</P>
</B><P>WE'VE KNOWN about this since 1975. He tried to sell his story in the U.S. but then it was all covered up. People in Derry have always known this. But none of this is being reported in the British press, and this is a question of who owns the press. </P>
<P>Some of it's shocking. Recently, a British Army general got up in court and said they had a "shoot-to-kill" policy for "colonial rioters," meaning Blacks in Africa, but that he didn't treat Northern Ireland as a colony.</P>
<B><P>WHY HAS the British government continued to deny what happened?</P>
</B><P>THEY THOUGHT they could keep it under the table. But the truth is out even with all the covering up. The British government should be very embarrassed, but this is how they do things. Even with this new inquiry, we're not getting all the documents, some things have just disappeared. My point of view is that the orders were given higher up. For example, by [then-Prime Minister] Ted Heath.</P>

