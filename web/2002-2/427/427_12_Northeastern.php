<P><font face="Times New Roman, Times, serif" size="5"><b>This fight is for free speech at Northeastern</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 25, 2002 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>, </P>
</B><P>On October 9, the Northeastern University chapter of the International Socialist Organization (ISO)--a student group on campus for more than six years--received an e-mail informing us that our group had been suspended. Without any warning, our room reservations were canceled, including the room for our meeting less than 24 hours later. </P>
<P>We were told that the reason for this move was that the ISO had failed to re-register for the fall--even though classes began only three weeks ago and most groups haven't yet re-registered. Also, we were told that we had put up posters "illegally" for our meeting entitled "The Bush Doctrine: Why You Should Oppose It."</P>
<P>This is a bureaucratic cover for their real aim--silencing campus activists. Only two months ago, the administration banned the Progressive Student Alliance from campus for its participation in a rally supporting janitors, who are on strike at Northeastern. Now the administration has set its sights on the ISO.</P>
<P>The only way to protect our rights is to use them, so we held our meeting as planned--in the Student Activities office, where the officials who banned our group work. Director of Student Activities Todd Shaver initially threatened to call campus police to kick us out--but was forced to back down in the face of more than 40 people refusing to leave his office.</P>
<P>In the end, we were allowed to finish our meeting in the room we had previously reserved. This was a victory for free speech at Northeastern--but it's only the beginning of a larger fight to ensure that students are never again denied their rights.</P>
<B><P>Matthew Boucher,</B> Boston</P>

