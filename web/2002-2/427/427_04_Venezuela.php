<P><font face="Arial, Helvetica, sans-serif" size="2"><b>VENEZUELA</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Bosses threaten another coup</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Bridget Broderick</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 25, 2002 | Page 4</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>VENEZUELAN PRESIDENT Hugo Ch&aacute;vez announced earlier this month that his government had stopped another right-wing coup attempt. But social tensions remain high as business leaders, military officers and corrupt union leaders--backed behind the scenes by the U.S. government--continue organizing for a confrontation that they hope will finally topple the populist president.</P>
<P>Last April, these anti-Ch&aacute;vez forces used a bosses' strike in the country's crucial oil industry and a mass opposition demonstration in the capital of Caracas as the pretext for a coup. Ch&aacute;vez was arrested, and the coup-makers installed a new government--led by the head of Venezuelan equivalent of the U.S. Chamber of Commerce--which immediately suspended the constitution and dissolved the National Assembly.</P>
<P>But splits at the top--and more importantly, a mass mobilization by Venezuela's poor--stopped the coup. Ch&aacute;vez returned to Caracas in triumph.</P>
<P>Ever since April, though, the opposition has been reorganizing. With Venezuelan and international companies pulling their money out of the country, the economy has been pushed deeper into crisis, with unemployment reaching new highs.</P>
<P>And the privately owned media regularly denounce Ch&aacute;vez for polarizing society and leading the country to economic ruin. They have whipped up a hysteria about the Bolivarian Circles--pro-Ch&aacute;vez grassroots organizations sponsored by the government--which they claim are heavily armed.</P>
<P>Yet it is clearly the middle and ruling classes that are re-arming themselves for another confrontation. On October 10, the opposition brought hundreds of thousands--the private media claimed more than 1 million--into the streets for what it called the "taking of Caracas." The main organization of the coup-makers--the misnamed Democratic Coordinating Committee--had called for a general strike on October 21 as <I>Socialist Worker</I> went to press.</P>
<P>But pro-Ch&aacute;vez forces appear to be more confident and organized today than in April. On October 13, they organized a hastily called march that brought at least as many people into the streets as the opposition demonstration a few days earlier.</P>
<P>And Bolivarian Circles have sprung up around the country, inspired by the mass protests of April. As this popular organizing gains ground, Ch&aacute;vez has become less conciliatory toward the opposition.</P>
<P>Yet he hasn't taken the radical steps that would be needed to resolve the economic crisis. While implementing relatively mild social reforms by investing state oil revenues in education, health care and small businesses, Ch&aacute;vez has refused to take the measures necessary to address the country's mass poverty--and he remains committed to paying a huge foreign debt to Western banks.</P>
<P>Ch&aacute;vez remains popular in comparison to the traditional elites and their Washington-inspired programs of economic austerity. </P>
<P>And he has gained support in Latin America with electoral successes for the left--above all in Brazil, where Workers' Party candidate Luiz In&aacute;cio "Lula" da Silva appears set to win an October 27 runoff election for president.</P>
<P>But Ch&aacute;vez's right-wing opponents--and their backers in Washington--aren't giving up. And that means that the stage remains set for conflict in Venezuela.</P>

