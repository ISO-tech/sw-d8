<font face="Times New Roman, Times, serif" size="5"><b>War cry of the chickenhawks</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 25, 2002 | Page 6</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>POLITICIANS ARE always ready to go to war. Except if they might be the ones doing the fighting. It turns out that many of the Bush administration's toughest "hawks" are "chickenhawks"--politically connected rich brats who were able to skip out on their own "patriotic duty" during U.S. wars.</P>
<P>The biggest chickenhawk of all is the Commander-in-Thief. Frat boy George W. Bush avoided going to Vietnam by having his daddy get a spot for him in the Air National Guard in 1968--jumping past more than 100,000 on the waiting list in front of him.</P>
<P>During the first several years of his six-year contract with the guard, Dubya took frequent time off to work on political campaigns. But even that proved too "taxing." For the last year of his service, he was AWOL--failing to show up at all.</P>
<P>Other members of the Bush administration have similarly dubious records. Take Vice President Dick Cheney--who opted for not one, not two, not three, but four student deferments to avoid military service. And when he ran out of those, he was able to swing a "paternity" deferment for fathers and expecting fathers. As Cheney says, he "had other priorities than military service."</P>
<P>But House Majority Whip Tom DeLay's excuse beats them all. DeLay graduated from the University of Houston at the height of the Vietnam War in 1970. But instead of doing his "patriotic duty," he chose to enlist in the war on cockroaches and termites--as the owner of an exterminator business.</P>
<P>DeLay claimed in 1988 that he had tried to volunteer for service in Vietnam--but that so many minority youths had volunteered for "well-paying" military positions to escape poverty that there was literally no room for "patriots" like him.</P>
<P>In his autobiography, Secretary of State Colin Powell wrote, "I am angry that so many of the sons of the powerful and well-placed&#133;managed to wangle slots in Reserve and National Guard units&#133;Of the many tragedies of Vietnam, this raw class discrimination strikes me as the most damaging to the ideal that all Americans are created equal and owe equal allegiance to their country." </P>
<P>You have to wonder if he ever talked to his chickenhawk boss about this "raw class distinction." Powell, of course, did "serve" his country in Vietnam--by helping to cover up U.S. war crimes like the My Lai massacre, among other things.</P>

