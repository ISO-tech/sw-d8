<font face="Times New Roman, Times, serif" size="5"><b>U.S. watchdog in the Middle East</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 25, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>FULLY ONE-THIRD of all aid given by the U.S. government to foreign countries goes to Israel--which has just 0.001 percent of the world's population. Why the imbalance? After all, Israel is an industrialized country with a per capita income rivaling Western Europe.</P>
<P>The usual explanation is that the U.S. has to give extra support to protect the only democracy in a Middle East dominated by dictatorships. But this claim both ignores the oppression at the heart of Israeli society--and disguises the true aims of the U.S.</P>
<P>Israel was formed as a Jewish-only state by hijacking the land of Palestinians. Today, the Palestinian minority that lives inside Israel is subjected to a maze of laws and restrictions relegating them to second-class citizenship in everything from schools to the right to own property.<B> </B>And Palestinians who live in the Occupied Territories of Gaza and the West Bank endure the crushing weight of military curfew, travel bans and economic devastation.</P>
<P>This isn't democracy. It's apartheid. So why does the U.S. government give Israel its unwavering economic, military and diplomatic support? Because Washington regards Israel as its key "strategic asset" in the oil-rich Middle East.</P>
<P>Shortly after Palestinians were driven from their homes in 1948 to found the Jewish-only state, Israel's <I>Ha'aretz </I>newspaper fleshed out the point in a 1951 editorial. "Israel is to become the watchdog," <I>Ha'aretz </I>wrote. "There is no fear that Israel will undertake any aggressive policy towards the Arab states when this would explicitly contradict the wishes of the U.S. and Britain. But if for any reason the Western powers should sometimes prefer to close their eyes, Israel could be relied upon to punish one or several neighboring states whose discourtesy to the West went beyond the bounds of the permissible."</P>
<P>In the 1967 war against neighboring Arab countries, Israel definitively proved to the U.S. that it would be not only loyal but fierce. Israel's military soundly defeated the armed forces of Egypt, Syria and Jordan--and seized the Golan Heights from Syria, the West Bank and East Jerusalem from Jordan, and the Gaza Strip and Sinai Peninsula from Egypt. </P>
<P>Between 1967 and 1972, U.S. aid to Israel jumped from $6.4 billion a year to $9.2 billion a year. In May 1973, Democratic Sen. Henry "Scoop" Jackson declared that "the strength and Western orientation of Israel on the Mediterranean and Iran on the Persian Gulf safeguards U.S. access to oil&#133;contain[ing] those irresponsible and radical elements in certain Arab states, who, were they free to do so, would pose a grave threat indeed to our principle sources of petroleum in the Persian Gulf."</P>
<P>The U.S. also relies on friendly relationships with certain Arab governments--from the secular regime in Egypt to the Islamic dictatorship in Saudi Arabia. But slavish support for U.S. interests by Arab leaders could enrage the mass of Arabs, who identify with the Palestinian struggle and the Iraqi people who have suffered a decade of U.S. bombs and sanctions.</P>
<P>Israel, on the other hand, can be counted on to be pro-U.S.--from top to bottom. U.S. aid to Israel sustains an apartheid state and the most brutal military force in the Middle East.</P>
<P>That's why opposition to U.S. imperialism must go hand in hand with ending U.S. aid to Israel and supporting the Palestinian struggle for self-determination.</P>

