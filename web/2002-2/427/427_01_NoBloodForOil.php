<font face="Times New Roman, Times, serif" size="5"><b>No war on Iraq</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 25, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WE SAY no to Washington's war on Iraq! That's the message people across the country will send George W. Bush on October 26.</P>
<P>Buses are booked in more than 125 cities to travel to Washington, D.C., for the first national demonstration against the Iraq war drive--and organizers are hoping for a turnout of more than 100,000. On the West Coast, tens of thousands will march in San Francisco on the same day.</P>
<P>These demonstrations will show that, despite months of lies and hysterical pronouncements, growing numbers of people are ready to stand up against Bush's war.</P>
<P>At every turn, Washington's hypocrisy has been on display. Like Bush's claim that his plan for "regime change" is about "liberating" the Iraqi people. After a war in 1991 that left up to 200,000 dead, and more than a decade of economic sanctions that have killed more than 1 million, it's clear that Washington doesn't care about ordinary Iraqis.</P>
<P>Nor are the invasion plans about Iraq's supposed arsenal of "weapons of mass destruction." When North Korea announced last week that it has a nuclear weapons program--something even Bush admits that Iraq doesn't have--the White House didn't begin whipping up a war frenzy. Maybe that's because there's no oil in North Korea.</P>
<P>Bush's plan for a new war on Iraq has nothing to do with "freedom" or "making the world a safer place." It's about expanding U.S. power in the Middle East--and grabbing control of more oil profits. It's time to step up our opposition to Washington's mad drive to war.</P>

