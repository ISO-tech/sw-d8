<P><font face="Times New Roman, Times, serif" size="4">Debate rages in Illinois over a plan for commutations</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Showdown over the death penalty</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 25, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ILLINOIS AGAIN took center stage in the national debate over the death penalty last week.</P>
<P>Nearly three years ago, Illinois Gov. George Ryan announced a moratorium on all executions in the state, declaring that the system is "broken." Now Ryan--who isn't running for re-election in November--is considering whether to commute the sentences of some or all of Illinois' death row prisoners before he leaves office.</P>
<P>The Illinois Prisoner Review Board began hearings last week into clemency petitions filed by 142 prisoners. The board will make confidential reports on each case, but the decision on commutations is up to Ryan alone.</P>
<P>As <I>Socialist Worker</I> went to press, the bulk of the highly emotional hearings had been completed--and the debate was at a fever pitch. On one side are prosecutors and police, who turned the hearings into a free-for-all by retelling the gruesome details of horrific murders and organizing family members of the victims to testify. On the other side are defense attorneys, anti-death penalty activists and family members of those on death row pleading for clemency.</P>
<P>Since the death penalty was reinstated in Illinois, 13 people have been released from death row after evidence of their innocence came to light. The nightmare of executing an innocent person is what convinced Ryan to declare a moratorium. "It was almost like flipping a coin," he said.</P>
<P>Ryan appointed a special commission to study the death penalty, and, in April, the panel issued its report, concluding that a massive overhaul of the system was necessary. The commission called for 85 separate reforms, ranging from videotaping confessions to disallowing convictions based on the testimony of jailhouse snitches. The vast majority of the prisoners asking for clemency couldn't have been sentenced to death under these guidelines, and attorneys focused on this in their arguments last week.</P>
<P>But it was the pro-death penalty forces that captured the media's attention. Prosecutors and police cynically exploited family members who lost loved ones to violent crime, forcing them to relive their suffering during testimony at the hearings.</P>
<P>This was nothing more than a way to deflect attention from the facts about Illinois' broken justice system. No one knows this better than Omar Saunders--one of four young African American men who spent more than a decade in prison for the brutal 1986 rape and murder of Lori Roscetti, before DNA evidence proved their innocence.</P>
<P>"I'm for what the governor intends to do because I did 15 years in prison for a crime that I did not commit," Saunders told <I>Socialist Worker</I> at an anti-death penalty press conference and rally in Chicago last week.</P>
<P>"For 15 years, the family of the victim thought that we were the killers. Our case is a textbook case. It shows that we were framed by Chicago police, the state's attorney and the Chicago crime lab. Had we been given the death penalty, we would be dead today. The families should really be mad at the state's attorney's office because they caused this dilemma."</P>
<P>The same prosecutors who made a name for themselves by railroading mostly poor minorities onto death row are trying to get Illinois' execution machine restarted. Activists have to keep up the pressure on Ryan to commute all death sentences in the state--and keep organizing to get rid of the death penalty once and for all.</P>

