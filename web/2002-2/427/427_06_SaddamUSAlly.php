<p><font face="Arial, Helvetica, sans-serif" size="2">October 25, 2002 | Page 6</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>SADDAM HUSSEIN killed his way to the top and used systematic terror to stay there. And he has the U.S. government to thank for it.</P>
<P>Saddam first came to prominence in 1959 when he tried to assassinate Abdul Kassim, an army officer who earned the hatred of Western governments when he led the overthrow of the British-installed monarchy in Iraq.</P>
<P>Four years later, Saddam's Ba'ath Party organized a coup against Kassim, with the help of the CIA. U.S. officials handed over lists of Iraqi socialists, and the Ba'athists hunted them down, leading to the massacre of an estimated 30,000 people.</P>
<P>When Saddam went to war against neighboring Iran in 1980, he had the support of the U.S. Actually, the West supplied weapons to both sides in the bloody war, which cost an estimated 1 million lives. But when Iran began to gain the upper hand in the late 1980s, the U.S. intervened decisively behind Iraq.</P>
<P>Only when Saddam stepped out of line by invading Kuwait in 1990 did Washington discover that its old ally was "the new Hitler."</P>

