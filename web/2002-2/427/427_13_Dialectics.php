<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Marxism and the dialectic of change</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 25, 2002 | Page 13</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MARXISM IS, in a nutshell, the theory and practice of working-class emancipation. Marxism is also a method of looking at the world. One of the most important foundations of Marx's method was dialectical thought.</P>
<P>From German philosophy--in particular that of Hegel--Marx took dialectics. Hegel was an idealist who believed that the whole of human history was a continual movement, through contradiction, toward absolute reason. He used an analogy from the physical world to illustrate his approach:</P>
<P>"The bud disappears in the bursting forth of the blossom, and one might say the former is refuted by the latter; similarly, when the fruit appears, the blossom is shown in its turn to be the false manifestation of the plant, and the fruit now appears as the truth of the plant. These forms are not distinguished from one another, they also supplant one another as mutually incompatible. Yet at the same time their fluid nature makes them moments of an organic unity in which not only do they not conflict, but in which each is as necessary as the other; and this mutual alone constitutes the life of the whole."</P>
<P>Marx argued that dialectics could be rescued from Hegel's idealist outlook--"turned on its head" to reveal a "rational kernel within the mystical shell." Formal logic, or what philosophers have sometimes called "metaphysics," sees things as static and unchanging: A always is equal to A, and can never become B. To the extent that this approach sees movement, it is one "thing" acting on some other "thing." To the extent that there are cycles, they are endlessly repeating cycles.</P>
<P>Dialectics rejects this approach, seeing things as in a constant process of coming into being and passing away. Moreover, it sees change not simply as a series of quantitative shifts that leaves the essence of the thing intact, but that quantitative changes can give way to qualitative leaps.</P>
<P>Finally, dialectics views fundamental change emerging from the fact that movement comes through contradictory forces acting against each other, creating an unstable unity. Dialectics allows for temporary states of stability or equilibrium, and also for qualitative leaps or breaks in which the equilibrium is broken and reforms on a new level.</P>
<P>A good example of the importance of seeing things dialectically is evolution. Until the 1860s, species were considered immutable--each created separately by God. Yet this is only an appearance. Closer study revealed that species evolve from each other, that there are not impassable barriers between species.</P>
<P>The movement of the planets around the Sun appears to be a perpetual cycle. But there are contradictory forces, not perceptible to the naked eye, which will inevitably bring about the system's catastrophic collapse or exploding apart.</P>
<P>Marx was most interested in applying dialectics to human history--in order to see it in its dynamic movement. Dialectics, he said, was "a scandal and an abomination to bourgeoisdom&#133;because it includes in its comprehension and affirmative recognition of existing things, at the same time also, the recognition of the negation of that state, of its inevitable breaking up; because it regards every historically developed social form as in fluid movement, and therefore takes into account its transient nature not less than its momentary existence."</P>
<P>Dialectics, Marx said, was "critical and revolutionary." The bourgeois social sciences cannot allow this approach to history. For them, human nature must be seen as a static, unchanging thing, in turn determining static, unchanging social relations.</P>
<P>To the extent that bourgeois social scientists accept change, it is only gradual. History as a process of more than gradual change, where contradictory forces bring about revolutionary leaps from one social state to another, cannot, of course, be admitted. To accept a dialectical approach to history is to admit that capitalism, like previous social forms, came into being, but will also pass away.</P>

