<P><font face="Times New Roman, Times, serif" size="4">The facts activists need to know</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Exposing their lies</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 25, 2002 | Page 5</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WHEN THE writer I.F. Stone used to speak to journalism students, he said that the most important thing to know about being a reporter could be summed up in two words: "Governments lie." Particularly when they want to sell a war.</P>
<P>The U.S. government has been at war with Iraq for more than a decade, and the lies have been piling up ever since. But Washington's war makers have kicked into high gear over the past few months--as George W. Bush and Co. try to sell us on another invasion of Iraq.</P>
<P>Here, <I>Socialist Worker</I>'s ANTHONY ARNOVE, editor of the South End Press book <I>Iraq Under Siege</I>--newly republished in an updated edition--exposes a decade's worth of U.S. lies and propaganda.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<B><P>MYTH</B><BR>
Saddam Hussein is "gaining the power to threaten our cities with annihilation."<BR>
--<I>New York Times</I> columnist William Safire</P>

<B><P>FACT</B><BR>
In truth, Iraq has no nuclear capacity and is years away from having the ability to turn fissile nuclear material--even if this could somehow be obtained--into a weapon.</P>
<P>Iraq also lacks any long-range missiles. Even the CIA reports that "Iraq is unlikely to test before 2015 any [intercontinental ballistic missiles] that would threaten the United States, even if United Nations (UN) prohibitions were eliminated or significantly reduced in the next few years."</P>
<P>The threat of Iraq's weapons program is being wildly exaggerated by politicians and the media to scare people into supporting a new war on Iraq.</P>

<B><P>MYTH</B><BR>
Saddam Hussein is "a man who loves to link up with al-Qaeda."<BR>
--George W. Bush</P>

<B><P>FACT</B><BR>
Bush is desperately trying to make a connection between Iraq and the September 11 attacks in the U.S.--though none exists. As Daniel Benjamin, who served on the National Security Council (NSC) from 1994 to 1999, wrote on September 30 in the <I>New York Times,</I> "Iraq and al-Qaeda are not obvious allies. In fact, they are natural enemies." An investigation by the NSC "found no evidence of a noteworthy relationship" between the two, Benjamin said. In fact, al-Qaeda militantly opposes the secular Iraqi government and Hussein's Ba'ath Party.</P>

<B><P>MYTH</B><BR>
The Iraqi government could "deploy chemical or biological weapons within 45 minutes of an order to do so."<BR>
--British Prime Minister Tony Blair</P>

<B><P>FACT</B><BR>
This claim, from the so-called "Blair Dossier" on Iraq, is backed up by evidence about the history of Iraq's weapons programs. But there's very little evidence in the "dossier" about the Iraqi government's current capabilities--because, say experts, there are very few capabilities left.</P>
<P>For example, the dossier says that Iraq "could" threaten the Persian Gulf region "if" it acquires the missiles it would need--which "might" happen within five years. Former UN weapons inspector Scott Ritter, who led dozens of teams through Iraq between 1991 to 1998, says that the UN destroyed between 90 and 95 percent of Iraq's weapons of mass destruction and its ability to manufacture them.</P>
<P>Even more stunning is the hypocrisy of U.S. and British claims about Iraq's chemical and biological weapons. The reason that Washington knows so much about Saddam Hussein's germ weapons programs before the Gulf War is that Washington supplied the materials--"a veritable witch's brew," according to author William Blum, citing a 1994 Senate report.</P>

<B><P>MYTH</B><BR>
"This is a regime that agreed to international inspections, then kicked out the inspectors."<BR>
--George W. Bush</P>

<B><P>FACT</B><BR>
UN weapons inspectors left Iraq in December 1998 in anticipation of the U.S. bombing of Iraq in December 1998--on Washington's orders. And, as the <I>Washington Post</I> reported on March 2, 1999, "United States intelligence services infiltrated agents and espionage equipment for three years into United Nations arms control teams in Iraq to eavesdrop on the Iraqi military." The information that the U.S. gathered was used to pick targets for the December 1998 bombing campaign.</P>

<B><P>MYTH</B><BR>
Before the 1991 Gulf War, Iraq massed troops along the border with Saudi Arabia, threatening an invasion.</P>

<B><P>FACT</B><BR>
In September 1990--four months before the Gulf War started--the Pentagon claimed that between 250,000 and 400,000 Iraqi troops and more than 1,500 tanks were amassed on Iraq's border with Saudi Arabia. While <I>USA Today</I> and other papers reported the claim as fact, the <I>St. Petersburg Times</I> in Florida decided to look for the evidence.</P>
<P>The <I>Times</I> obtained commercial Soviet satellite images of the area--and found nothing. "It was a pretty serious fib," says Jean Heller, the reporter who broke the story. "That [buildup] was the whole justification for Bush sending troops in there, and it just didn't exist," Heller told the <I>Christian Science Monitor</I>.</P>

<B><P>MYTH</B><BR>
Iraqi soldiers ripped Kuwaiti babies out of incubators when they invaded Kuwait in August 1990.</P>

<B><P>FACT</B><BR>
In October 1990, members of Congress listened to the powerful testimony of a 15-year-old Kuwaiti "refugee" named Nayirah. In tears, Nayirah described how she had witnessed Iraqi troops steal incubators from a hospital, leaving 312 babies "on the cold floor to die."</P>
<P>When the Senate voted to give support George Bush Sr.'s war--by a margin of only five votes--seven senators recounted Nayirah's story in justifying their "yes" vote. The president himself repeated the story several times.</P>
<P>There's just one problem: It wasn't true. Nayirah's false testimony was part of a $10 million Kuwait government propaganda campaign managed by the public relations firm Hill and Knowlton. Rather than working as a volunteer at a hospital, Nayirah was actually the daughter of the Kuwaiti ambassador to Washington.</P>
<P>"We didn't know it wasn't true at the time," claims Brent Scowcroft, Bush's national security adviser. But, he admitted, "it was useful in mobilizing public opinion."</P>

<B><P>MYTH</B><BR>
"America is a friend to the people of Iraq."<BR>
--George W. Bush</P>

<B><P>FACT</B><BR>
After being subjected to the most comprehensive economic sanctions in world history and destruction of the country's infrastructure by U.S. bombing, the people of Iraq know what a lie this is. As <I>New York Times</I> Nicholas Kristof wrote in an unusually truthful report from Baghdad, "While ordinary Iraqis were very friendly toward me, they were enraged at the U.S. after 11 years of economic sanctions."</P>
<P>"U.S. bombing of water treatment plants, difficulties importing purification chemicals like chlorine (which can be used for weapons), and shortages of medicines [have] led to a more than doubling of infant mortality, according to the UN Food and Agriculture Organization," Kristof acknowledged.</P>
<P>Iraqis know that another invasion will lead to more civilian casualties. And they know that for years, Washington backed Saddam Hussein, despite his brutality--because he was a "friend." The U.S. even gave its permission for Saddam's forces to suppress a rebellion at the end of the Gulf War, maintaining him in power.</P>

<B><P>MYTH</B><BR>
The Bush administration is "driven not by any lust for global domination, but by out-and-out Wilsonian idealism: we want to make the Middle East safe for democracy."<BR>
--William Safire</P>

<B><P>FACT</B><BR>
As the <I>New York Times</I> reported October 11, "The White House is developing a detailed plan, modeled on the postwar occupation of Japan, to install an American-led military government in Iraq if the United States topples Saddam Hussein. In the initial phase, Iraq would be governed by an American military commander--perhaps Gen. Tommy R. Franks, commander of United States forces in the Persian Gulf, or one of his subordinates--who would assume the role that Gen. Douglas MacArthur served in Japan after its surrender in 1945&#133;For as long as the coalition partners administered Iraq, they would essentially control the second-largest proven reserves of oil in the world, nearly 11 percent of the total."</P>
<P>Some democracy. The White House's talk about controlling Iraq's oil fields reveals what this war is really about. The rhetoric about weapons, democracy and human rights is a sham--just as it was in 1991.</P>
<P>But if we expose these lies, we can mobilize an opposition to Bush's war--and link the fight against the war on Iraq to the fight for real democracy, at home and in the Middle East.</P>

