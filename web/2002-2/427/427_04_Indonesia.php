<P><font face="Arial, Helvetica, sans-serif" size="2"><b>INDONESIA</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Bush claims al-Qaeda link to attack in Bali</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>U.S. uses bombings to expand its war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 25, 2002 | Page 4</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WITHIN HOURS of the horrific bombings in a resort town on the Indonesian island of Bali, the U.S. government was sending a message: Join our "war on terrorism," or suffer the consequences.</P>
<P>"Together, we will fight terror so as to keep the peace and to make the world more free," George W. Bush said in a videotaped message to the government of Australia, which accounted for half of the nearly 200 casualties. But the new anti-terrorism laws pushed through by the Indonesian government will do anything but make the country "more free."</P>
<P>"Police can detain anyone strongly suspected of acts of terrorism based on initial evidence for as long as seven days," reads the emergency decree enacted by President Megawati Sukarnoputri. "Any person found intentionally using violence or a threat of violence that would create terror or unrest among the masses&#133;faces the death penalty."</P>
<P>Likewise, in the Philippines, government officials are using the Bali attacks and recent blasts in Zamboanga City and Manila to push through draconian "anti-terrorism" legislation. The bills would set up a national identification system and allow for arrests without warrants and further restrictions on democratic rights.</P>
<P>These expanded police powers are straight out of the 32-year dictatorship of Gen. Suharto, who was overthrown in a mass uprising in 1998. Megawati was a well-known opposition figure under Suharto, and she was elected first vice president and then president after his overthrow. But Megawati has cut deals with many of the same military figures that called the shots under the dictatorship.</P>
<P>And the big unasked question now about the bombings in Bali is what role the military played. Investigators believe that the bombers used C-4 plastic explosives--a sophisticated weapon that only the army's special forces units typically have access to in Indonesia.</P>
<P>After Suharto's overthrow, Indonesia's generals encouraged the rise of Islamist groups to counter political radicalization in the cities and the upsurge of independence movements throughout the country.</P>
<P>"If you scratch below the surface of any radical Islamic group in Indonesia, you will find the hand of the military at work," Sidney Jones, head of the Indonesian office of the International Crisis Group, told Britain's <I>Guardian</I> newspaper. "And with many of them, you don't really have to go beneath the surface."</P>
<P>Under pressure from Washington, Indonesian police arrested 64-year-old Islamic cleric Abu Bakar Bashir, leader of the radical Muslim group Jemaah Islamiyah, while he was in the hospital for cardiac and breathing problems. The U.S. is painting Jemaah Islamiyah as the main ally of the al-Qaeda network in Southeast Asia. But Bashir is known to have close ties to leading figures in Indonesia's military and police.</P>
<P>If whatever group is behind the bombings turns out to have a connection to the generals, then the U.S. government will bear responsibility for the nightmare in Bali. Washington has been re-establishing its connections to the military since Suharto's downfall--and opening the pipeline of arms and training that turned Indonesia's army into one of the most brutal human rights abusers in the world.</P>
<P>But don't expect the Bush gang to have second thoughts. They plan to use the attacks in Bali to expand their "war on terror"--no matter what the cost to ordinary people in Indonesia, the Philippines or anywhere else.</P>

