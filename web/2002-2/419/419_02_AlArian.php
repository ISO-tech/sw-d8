<P><font face="Times New Roman, Times, serif" size="5"><b>Sami Al-Arian faces new threat</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 30, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>SAMI AL-ARIAN is facing a renewed threat to his job. University of South Florida (USF) President Judy Genshaft announced last week that the school has asked a court if it can fire the computer science professor.</P>
<P>Al-Arian became the target of a witch-hunt last fall after he appeared on Fox News' <I>O'Reilly Factor,</I> where right-wing host Bill O'Reilly branded him a "terrorist." O'Reilly based his outlandish accusation on the fact that Al-Arian had been investigated&#150;but never charged&#150;by the Justice Department six years before for his work with a pro-Palestinian foundation. USF administrators suspended Al-Arian with pay. But that wasn't enough for the board of trustees, which tried to fire him in December.</P>
<P>Support for Al-Arian poured in, however, including a vote by the USF faculty union and faculty senate to support him, and the administration was forced to back off. Now, Genshaft is not only trying to get a court to do her dirty work for her, but she's stepping up the smear campaign against Al-Arian.</P>
<P>She claimed last week that Al-Arian participated in a 1991 conference at which "money was raised for causes later associated with terrorist activities." Genshaft will use any sleazy tactic to try to get rid of him. "I'm an Arab, I'm Palestinian, I'm a Muslim," Al-Arian told a press conference last week. "That's not a popular thing to be these days."</P>

