<P><font face="Times New Roman, Times, serif" size="5"><b>The ugly reality facing Argentina's poor</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 30, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>This has to be a new low. Regular readers of <I>SW</I> will know about the economic devastation hitting Argentina right now, where unemployment and underemployment is running at 45 percent.</P>
<P>In a sane world, the government would be increasing welfare for the unemployed and fixing the economy to help ordinary people. But instead, what's on offer for Argentina's unemployed is the latest--and sickest--version of "reality TV."</P>
<P>From Monday to Friday at 5 p.m., two unemployed Argentinians compete for a job on the new hit show <I>Human Resources</I>. Each contestant has to tell their story and convince the viewers why they deserve the job more. At the end of the two interviews, viewers choose who should get the job.</P>
<I><P>El Proceso,</I> Mexico's leading newsweekly, recently reported how the producers purposefully rig the show so that the most destitute people get on, ensuring that the drama runs high. "I've got a good one," beams producer Ana Maria Torres Castro happily. "He's from Rosario, his wife had polio, and he sells phone cards on the street."</P>
<P>On a lucky day, the employer will make an offer to both contestants. If not, though, both go home "winners"--the runner-up at least gets health insurance for six months.</P>
<P>Nestor Ibarra, the host, tried to justify this insanity: "It's crazy. If the final selection were made by the company, the criteria would be more practical. They would choose the person with fewer children, because then they'd have to pay less family wages, or the one who lives closer, who has fewer problems. The audience, on the other hand, will choose the one who needs the job more. Which of the two systems is wiser?"</P>
<P>Neither. The whole thing exposes the rot of this society especially when it's in crisis, and helps to explain why Argentinians continue to fight so hard for their future. If they don't, insults like <I>Human Resources</I> will be all they have.</P>
<B><P>Jeff Bale,</B> Washington, D.C.</P>

