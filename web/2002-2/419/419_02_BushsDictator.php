<P><font face="Times New Roman, Times, serif" size="5"><b>The dictator that Bush loves</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 30, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>PAKISTAN'S DICTAtorial president, Pervez Musharraf, made a bold new power grab last week. And that's just fine with George W. Bush.</P>
<P>Musharraf, an army general who seized power in a 1999 coup, declared that he was amending Pakistan's constitution so that he could unilaterally dissolve the elected parliament and appoint any military leaders and Supreme Court justices he pleases.</P>
<P>But the White House owes Musharraf for cutting Pakistan's ties to the Taliban government in Afghanistan and cooperating with the Pentagon's invasion of Central Asia. "He's still tight with us in the war against terror, and that's what I appreciate," Bush declared to reporters.</P>
<P>Incredibly, Bush essentially endorsed Musharraf, even as a top State Department official was on his way to Pakistan to express U.S. "concern." "He understands that we've got to keep al-Qaeda on the run," Bush said. "And I appreciate his strong support."</P>
<P>Dubya did manage to stutter out a vague objection to, you know, military dictatorship. "Obviously, to the extent that, you know, our friends promote democracy, that's important," he said. As long as they promote the interests of the U.S.</P>

