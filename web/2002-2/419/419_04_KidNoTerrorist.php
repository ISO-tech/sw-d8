<P><font face="Times New Roman, Times, serif" size="4">"The system is going to steal a part of his life"</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>This kid isn't a terrorist</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 30, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>I recently went to see a student of mine in jail. I was shocked. He hadn't seen sunlight in over a month, a gang had shaved his head, and he hadn't been sleeping because he's been drinking only coffee.</P>
<P>A bunch of Piccalo Pete firecrackers exploded in his backpack at school two days before graduation. The firecrackers were wired together and had a timing device on them, but when they exploded, no one was hurt. Now, Danny is being charged with "possession of explosives"--a felony with a two-year minimum sentence. </P>
<P>He has no prior record. In fact, he was a model student liked by all of his teachers. I gave him the "Most Improved Senior" award this year. He was trying to finish with good grades so that he could move to Colorado to help his father there.</P>
<P>The administration at my school and the prosecution have used the word "terrorist" to talk about this "crime," even though pranks with small fireworks have been widespread at our school all year. Bail was set at $250,000.</P>
<P>County jail in Los Angeles is scary and depressing. Danny told me that he's serving time with people who stole bananas and one man who "stole" water bottles. </P>
<P>At the same time, gangs in the jail are a constant threat. It's confusing to know what to do. Danny's lawyer thinks that he can get him off with three months, but Danny would have to plead guilty to a lesser felony and be on parole for five years.</P>
<P>Most of all, I'm angry that for a stupid prank, the system is going to steal a part of the life of an 18-year-old who I always admired for his good-heartedness to his fellow students and his intellectual curiosity. </P>
<B><P>Sarah Knopp, </B>Los Angeles</P>

