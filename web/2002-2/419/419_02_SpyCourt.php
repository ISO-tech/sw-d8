<P><font face="Times New Roman, Times, serif" size="4">Even secret judges reject his police-state plan</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Spy court says no to Ashcroft</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 30, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>YOU KNOW that John Ashcroft must be really out of control when even a secret U.S. spy court objects to his police-state schemes.</P>
<P>The court that oversees the Foreign Intelligence Surveillance Act (FISA) rejected--in a ruling made in May that was publicly released last week for the first time in the court's history--a secret request from the Justice Department to allow more evidence-sharing between federal counterintelligence investigators and criminal prosecutors.</P>
<P>The FISA court generally operates in secret and is responsible for approving warrants to eavesdrop on people suspected of espionage or terrorism. That makes the decision to publicize the ruling quite a surprise. Even more amazing, the ruling describes an "alarming number of instances" dating back to the Clinton administration in which the FBI might have acted improperly.</P>
<P>Under the current law, agents have to show probable cause that the subject of a search is an agent of a foreign government or terrorist group. But according to the court, in September 2000, the Justice Department admitted to "misstatements and omissions of material facts" in 75 applications for search warrants and wiretaps. The ruling also reported "similar misstatements in another series of FISA applications" dating from March of last year.</P>
<P>With this record of lies and dirty tricks, the spy judges apparently decided that new procedures proposed by Ashcroft under the USA PATRIOT Act would have given prosecutors too much control over investigations--and would open the door for even more government misuse of intelligence information.</P>
<P>Ashcroft's Justice Department immediately claimed that the court was tying the government's hands behind its back and hindering the fight against terrorism. The Feds filed an immediate appeal--the first formal challenge to the FISA court in its 23-year history.</P>
<P>Meanwhile, the spy court left the door open for lawmakers to update the USA PATRIOT Act to solve the problems that were raised in the ruling. Now, Congressional hacks from both parties are talking about amendments to help strengthen the "war on terrorism."</P>
<P>As Gregory Nojeim of the American Civil Liberties Union said, "The intelligence court's opinion shows that the Department of Justice has abused the intelligence powers it already has and should not be showered with more until it addresses the problems the court identified." But with fanatics like Bush and Ashcroft running the show, you can bet that won't be happening any time soon.</P>

