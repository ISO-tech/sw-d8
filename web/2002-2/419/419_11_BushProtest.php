<P><font face="Times New Roman, Times, serif" size="5"><b>Portland cops attack anti-Bush protesters</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul Dean</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 30, 2002 | Pages 10 and 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>PORTLAND, Ore.--Police used pepper spray, batons and even rubber bullets in an outrageous attack on a crowd of some 2,000 people who turned out August 22 to protest George W. Bush.</P>
<P>The unexpectedly large demonstration took place outside the Hilton Hotel, where Bush was speaking at a fancy fundraiser for Sen. Gordon Smith (R-Ore.). While Republicans paid $25,000 a pop to have their picture taken with Dubya, protesters rallied outside to send their anti-Bush message, focusing especially on Bush's recently announced plan to increase logging and the administration's war drive against Iraq.</P>
<P>Police had blocked off a large area around the hotel for protesters, and the rally was peaceful. But then, cops in full riot gear came around the barriers.</P>
<P>After declaring a state of emergency--which was barely audible to the protesters--police pushed into the crowd, wildly firing pepper spray. "Maybe the ones in front were warned to move, but I didn't hear any warning," one protester told the left-wing "Truthout" Web site. "It had been a peaceful protest. Suddenly, the police came forward spraying pepper spray. A man with an infant in a backpack got hit real good. The baby's face was so red I thought it had quit breathing."</P>
<P>Police cars came around the back and drove directly into the demonstration. Some protesters climbed onto the hood of the cars to try to stop them from running over people. The cops even began firing various kinds of rubber bullets, hitting several demonstrators.</P>
<P>And the response of Portland Police Chief Mark Kroeker? "In totality, I'm pleased with the way it went," he told reporters.</P>
<P>Several times, police declared an emergency and threatened to arrest anyone who didn't disperse immediately. But protesters refused to be intimidated, and the rally was still going several hours later. Afterwards, demonstrators marched downtown, chanting, "Whose streets? Our streets!"</P>
<P>The determination of activists here sent a clear message to Bush: You won't get away with your plans for war without a fight.</P>

