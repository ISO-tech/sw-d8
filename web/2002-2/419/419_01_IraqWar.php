<P><font face="Times New Roman, Times, serif" size="4">These men don't care how many more Iraqis die</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>No war on Iraq!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 30, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THEY MAY not be able to prove their claims about Iraq possessing "weapons of mass destruction." But the war party in Washington is getting ready anyway for a new campaign of terror against the people of Iraq.</P>
<P>George W. Bush, Dick Cheney and Donald Rumsfeld are beating the war drums to drown out any critics. They'll cynically use the anniversary of the September 11 attacks to justify plans to kill countless more innocent Iraqis. "If the United States could have preempted 9/11, we would have, no question," Cheney said at a convention of the Veterans of Foreign Wars this week. "Should we be able to prevent another, much more devastating attack, we will, no question."</P>
<P>There are questions about the Bush gang's invasion plans within the Washington establishment itself--and top congressional leaders are angry at the White House's arrogant claim that it doesn't need a vote in Congress to go to war, though the U.S. Constitution says just the opposite.</P>
<P>But that isn't stopping the Bush gang. "Regime change in Iraq would bring about a number of benefits to the region," Cheney declared. "When the gravest of threats are eliminated, the freedom-loving peoples of the region will have a chance to promote the values that can bring lasting peace."</P>
<P>Translation: the U.S. aims to terrorize the Middle East into submission.</P>
<P>But on what grounds does the U.S. claim the right to carry out a "regime change"? If lack of democracy is a good enough reason, then other countries could claim the right to wage war on the U.S. to remove its unelected president.</P>
<P>Weapons of mass destruction? Refusal to submit to weapons inspections? Washington has by far the world's biggest arsenal of nuclear, biological and chemical weapons. And the Bush gang only recently finished wrecking another international treaty in order to prevent inspectors from gaining access to U.S. facilities.</P>
<P>This is a country where one of the president's closest advisers is willing--even eager--to think the unthinkable. "No strategist would reject, in principle, using nuclear weapons against Iraq," said Richard Perle.</P>
<P>This is madness. But in Bush's world, the sole superpower calls the shots--at any cost. If any country truly deserves the title of "mortal threat," it's the United States. We have to organize an opposition that can stand up and say, "No war on Iraq!"</P>

