<P><font face="Times New Roman, Times, serif" size="4">Nonunion workers at Cody Steel are out on strike</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"We're sticking together"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nate Moore</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 30, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>BURLINGTON, Vt.--Non-union ironworkers walked off the job here August 15 over unfair labor practices committed by their employer Cody Steel Erectors (CSE).</P>
<P>Individual workers had been continually harassed and interrogated by their bosses simply for discussing their desire to unionize--which is illegal according to federal law. "The company is committing unfair labor practices, and they are trying to intimidate us," striker Tom Foy told <I>Socialist Worker</I>. "We want to be treated fairly."</P>
<P>The walkout became a strike with the help of Ironworkers Local 474 of Vermont. Workers wore shirts with the slogan "CSE stop the ULPs" (unfair labor practices). CSE responded by calling in 15 Spanish-speaking replacement workers from North Carolina.</P>
<P>Dave Kazcynski, a member of Local 474, responded by entering the worksite August 19 during lunchtime with this writer and a translator to try to speak with the replacement workers and win their solidarity. During this discussion, the CSE boss, Ski Cody, approached the replacement workers and placed his index finger over his mouth, indicating that they shouldn't speak.</P>
<P>CSE raked in over $22 million in profits last year--but most of their employees don't even have adequate health benefits even though theirs is the most dangerous job in the construction field. An adequate retirement plan is also lacking. "I've been with CSE for five years and in the business for 20 years, and all I've got is $2,200 in a 401k plan," striker Wayne Ring II said. "This is why we are fighting--to bring up the standard of living for everyone."</P>
<P>About 50 unionized rank-and-file ironworkers from all over New England traveled to Burlington August 22 to show support for the struggle. "This is all about brotherhood, we've got to stick together. If we don't, nobody else will," said Deb Doucette, a member of Ironworkers Local 7 in Boston.</P>
<P>About 70 workers and supporters marched to the office of Burlington Mayor Peter Clavelle, who refused workers' request that he call CSE on the spot but promised to do so later. "We've got to get back to grass-roots organizing," said ironworker Pat Blackburn. "This is a two-class system divided between rich and poor."</P>
<P>"It was great seeing everyone come out and be supportive," striker Caleb Foote said. "It put fire under my feet, and I am motivated to continue." Added striker Tom Foy: "We are in it for the long haul."</P>

