<P><font face="Times New Roman, Times, serif" size="5"><b>Pentagon orders the enemy to lose</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 30, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE PENTAGON couldn't very well allow Saddam Hussein to win--even if it was just a war game. So military officials re-floated the better part of the U.S. Navy, sunk in a simulated surprise attack--and essentially ordered the enemy to lose.</P>
<P>Paul Van Riper, a retired Marine Corps general who had commanded the enemy forces, blew the whistle on the rigged exercises, held in early August. The three-week-long war game--the biggest in Pentagon history, with a price tag of $250 million--was "almost entirely scripted to ensure a [U.S.] win," Van Riper told the <I>Army Times</I>.</P>
<P>Van Riper protested by quitting as head of the simulated armed forces of an unnamed Middle Eastern country, which happened to bear a strong resemblance to Iraq. "We were directed&#133;to move air defenses so that the Army and Marine units could successfully land," Van Riper said. "We were simply directed to turn them off or move them&#133;So it was scripted to be whatever the control group wanted it to be." </P>
<P>The <I>Army Times</I> concluded that, as commander of a low-tech army, Van Riper appeared to have repeatedly outwitted U.S. forces--for example, sending out orders with motorcycle couriers to evade sophisticated electronic eavesdropping equipment.</P>
<P>"When the U.S. fleet sailed into the Gulf, [Van Riper] instructed his small boats and planes to move around in apparently aimless circles--before launching a surprise attack which sank a substantial part of the U.S. Navy," Britain's <I>Guardian </I>newspaper reported. "The war game had to be stopped and the American ships 'refloated' so that U.S. forces stood a chance." </P>
<P>The exercises were supposed to test experimental new tactics and doctrines championed by Defense Secretary Donald Rumsfeld as part of his plan for "military transformation." But Van Riper said that Rumsfeld's program amounted to a whole lot of hot air.</P>

