<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Don't be fooled by the argument about Iraq invasion plans</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>They're debating how and when</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 30, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>YET ANOTHER leading voice of the U.S. political establishment has told the Bush administration to slow down its race to war. James Baker III--the former secretary of state under Papa Bush and head of Dubya's smash-and-grab operation for stealing the 2000 election in Florida--wrote a featured op-ed article in the <I>New York Times</I> last weekend advising the White House against "going it alone" in an assault to topple Iraqi leader Saddam Hussein.</P>
<P>The growing questioning of the Bush administration--most loudly from Republicans, not the Democratic "opposition" in Congress--is a sign of a real disagreement in Washington. But no one should think that this is a debate about war or peace. It's a question of how and when. Baker himself made this clear. "[R]egime change in Iraq is the policy of the current administration, just as it was the policy of its predecessor," he wrote. "That being the case, the issue for policymakers to resolve is not whether to use military force to achieve this, but how to go about it."</P>
<P>Make no mistake. James Baker and the Bush gang are old buddies, and they'll eventually agree on "how to go about it." During the months before the first Gulf War against Iraq in 1991, there were plenty of "voices of caution" in Washington. But after Papa Bush put together a propaganda campaign and got the United Nations (UN) to endorse Operation Desert Storm, the establishment "opposition" fell in line.</P>
<P>Those in the antiwar movement who had conceded ground to the "cautious" opponents of war--to "consult" with the UN or to "let sanctions work" before bombing, for example--ended up disoriented and weakened when their fair-weather friends jumped on the pro-war bandwagon.</P>
<P>Likewise, we shouldn't concede to establishment "doubters" today--as, for example, the editors of the liberal <I>Nation</I> magazine do when they ask in an editorial: "Why engage in a risky and potentially calamitous invasion of Iraq when the existing strategy of 'containment'&#133;has clearly succeeded in deterring Iraqi adventurism for the past 10 years&#133;?"</P>
<P>"Containment" is an awfully generous description of what the U.S. has done to Iraq for the last decade--alternating new bombing campaigns with an iron regime of sanctions that has killed many times more Iraqis by starving the country of food and the most basic goods.</P>
<P>The question that we should be asking is this: By what right does the U.S. government claim to decide who will govern Iraq? This is democracy? The politicians may hyperventilate about Saddam Hussein and weapons of mass destruction in Iraq, but the U.S. government is guilty of violence and oppression on a vastly greater scale.</P>
<P>Antiwar activists will have a growing audience of people with doubts about Bush's war drive in the weeks to come. We have to start now to educate this audience about the history of Washington's imperialist adventures and its real aims in the Middle East. That's the way to turn the doubts into an active opposition that can stand up to the U.S. war machine.</P>

