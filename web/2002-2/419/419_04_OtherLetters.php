<p><a name="1997"></a><font face="Times New Roman, Times, serif" size="4"><b>How the Teamsters strike in 1997 beat UPS</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>In 1997, United Parcel Service (UPS) was hit with the first national strike of its Teamsters employees. UPS was trounced on every front. It lost the public relations battle, with the public supporting workers 2-to-1 over the company. It lost $750 million in lost business. And most importantly, it lost the strike itself, conceding on the key issues. </P>
<P>UPS workers won the best contract in decades. The company was taken off guard largely because it assumed that its workers would not fight back. After all, the union leadership had a long history of collaborating with management. But contrary to management expectations, 95 percent of the workers did not cross the picket line and showed their solidarity.</P>
<P>Since then, however, the company has learned the lessons of the strike. UPS hired a new public relations firm, and this year's UPS advertising campaign, "What Can Brown Do For You?" removes UPS workers from the ads. </P>
<P>For UPS, its drivers are its public face. In 1997, the company could not demonize its workers and call them "greedy" for going on strike because that would have affected business. This year, UPS decided to undo this image.</P>
<P>But a strike was far from Teamsters President James Hoffa's mind. Most union leaders are hesitant to call strikes, preferring the negotiating room where they have more control. Even Ron Carey, a union reformer who led the 1997 strike, was initially reluctant to call for a walkout. It was rank-and-file pressure and raised expectations that led Carey to call the strike.</P>
<P>The lessons for workers are clear. Regardless of who is in the leadership of the union, it is rank-and-file organization that is crucial to winning decent contracts from employers. While UPS spent huge sums of money gearing up for the 2002 contract and drawing out the lessons for its side, our side has not done the same. </P>
<P>UPS workers cannot rely on Hoffa. They need to rely on their own organization and activity to wage a successful fight against UPS. </P>
<B><P>Dina Roy,</B> Greensboro, N.C.</P>

