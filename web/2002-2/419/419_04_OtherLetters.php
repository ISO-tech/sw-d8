<P><font face="Times New Roman, Times, serif" size="4"><b>Trained to kill by the U.S. military</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>A string of murders over the past two months has left Armed Forces officials at Fort Bragg, N.C., puzzled.</P>
<P>The rash of killings started on June 10 when Sgt. 1st Class Roberto Nieves shot and killed his wife, and then himself, just two days after returning from Afghanistan. On June 29, the wife of Master Sgt. William Wright was strangled. Wright, who had returned from Afghanistan a month earlier, was charged with her death. Then, on July 9, Sgt. Cedric Ramon stabbed his estranged wife and set fire to her home. July 19 saw the deaths of two more people: Sgt. 1st Class Brandon Floyd gunned down his wife, then turned the gun on himself. </P>
<P>These six deaths are not the first seen at Fort Bragg--one of the country's largest and most important military installations. They mirror several other murders committed by Fort Bragg soldiers since the 1970s. </P>
<P>In the wake of these violent outbursts, there is expected to be a review of Armed Forces policy on the "handling" of the families of soldiers returning from Afghanistan--to "hopefully avoid future problems," according to retired Air Force Col. John Carney.</P>
<P>But these seemingly random acts of violence are not unconnected. These are soldiers who were taught to murder, maim and participate in a slaughter in Afghanistan. Is it any wonder that sooner or later they bring that violence home? </P>
<P>These murders are a product of the changes that that a person undergoes when confronted with the horrors of war. Unless we do away with the system that fosters such violence, these sorts of cases will persist.</P>
<B><P>Justin Southerland,</B> Greensboro, N.C.</P>

