<p><a name="smokers"></a><font face="Times New Roman, Times, serif" size="4"><b>How they make smokers pay</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>I'm outraged by the moral crusade against smoking--and think that socialists ought to oppose it, because it is an attack on working-class people.</P>
<P>Of course, smoking is unhealthy, and cigarette companies rank among the most loathsome peddlers of disease and lies in a rather crowded field. But these companies have teamed up with Hollywood, the U.S. government (which used to distribute free smokes to every soldier) and advertising snake-oil salesmen for decades to sell us this addiction. </P>
<P>Now, working people and the poorest Americans--who comprise the overwhelming majority of the 25 percent who smoke--are being forced to pay for the health care crisis created by the government and Corporate America.</P>
<P>Unfortunately, many progressives support the smoking bans and higher taxes that have driven cigarette prices across the country above $4 a pack and an astonishing $7 in New York City. But for people whose lives are filled with tension, who have too little time and money for leisure, who can't go to a gym or chat with a massage therapist, smoking is one of the few accessible, if fleeting, pleasures.</P>
<P>Perhaps in a better world, people would not feel the need to indulge in unhealthy habits that can kill them. But let's face it, the U.S. government and corporate fat cats are a self-indulgent and arrogant class of moralizers who are addicted to profits at any human cost. </P>
<P>First, we need to break them of their nasty habits of greed and power. Then we can create a healthier world for us to live in.</P>
<B><P>Sherry Wolf,</B> Chicago</P>

