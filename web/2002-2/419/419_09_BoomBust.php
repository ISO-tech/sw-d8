<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>What's behind the boom-bust cycle?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 30, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CAPITALISM'S BOOM-bust cycle is central to its operation, not some temporary aberration. It is the product of the unplanned, anarchic nature of capitalism, whereby periods of hyped-up investment as sales increase and profits rise are followed by periods of overproduction leading to falling profits and layoffs.</P>
<P>Marx was careful to point out that this was not overproduction above human need, but rather overproduction of what can be sold profitably. A rational system based upon planned production and distribution could always make use of extra machines and products to improve the quality of human life.</P>
<P>Under capitalism, growth can only be resumed after a certain amount of capital is allowed to be destroyed and/or drastically devalued, a process that brings much human misery in its wake. Then the whole cycle begins again. Underlying this cycle is a more long-term pattern that Marx identified as a "tendency for the rate of profit to fall," which could lead to the slumps getting worse and the booms shallower.</P>
<P>Like all living systems that go through cycles, the cycles of capitalism's birth are different than the cycles in its old age. This is true for two reasons.</P>
<P>The first reason is that competitive capitalism, over time, has turned into its opposite. The relatively freer competition of early capitalism between smaller competing units has given way to the concentration and centralization of capital into ever-larger units.</P>
<P>Alongside this process is a trend toward a greater labor productivity. The need to increase market share compels each capital to invest in technology that reduces the amount of labor time necessary to produce each individual unit, and therefore its cost.</P>
<P>The capitalist who is the first to use labor-saving technology is able to undersell his or her competitors, at least until they adopt similar methods. This process, driven by each capitalist's drive for short-term profits, leads in the long term to the shrinking role of the very element in the production process that produces extra value (the source of profit)--labor.</P>
<P>We explained in an earlier article that living labor is the source of value, that the amount of labor time necessary to produce a commodity determines its value. Profit is the difference between what capitalists spend on all the elements necessary for production--from machinery to labor power--and what they get back when they sell the product.</P>
<P>The capitalist likes to think that profits are simply the reward for bringing all these elements together to produce a thing for sale. But machines do not produce extra value. They merely pass on all, or part, of the labor-value that was already expended on them in their production. They are, in Marx's words, "dead labor." Only living labor adds new value over and above its own cost.</P>
<P>Marx called the difference between necessary labor time (the time necessary to match wages) and surplus labor time (the time appropriated without compensation by the employer) the rate of exploitation. This rate of exploitation under capitalism is constantly increasing, because with increased productivity, the amount of time necessary to reproduce the necessities of workers goes down.</P>
<P>But capitalists measure their success not by the rate of exploitation, but by the rate of profit, that is, how much extra value they're getting over their total expenditure. But as productivity increases, labor becomes a relatively smaller and smaller component of production, and machinery a relatively larger and larger component.</P>
<P>So the short term gain that individual capitalists get by investing in new machinery in allowing them to undersell their competitors leads over time to an increasing ratio of expenditure on machinery compared to labor.</P>
<P>The result is that, over time, the rate of profit falls. But capitalism is driven by the pursuit of profit. Ironically, the very process of accumulation that drives capitalism is also a process that undermines it!</P>

