<P><font face="Times New Roman, Times, serif" size="4">Bush's "anti-terror" ally in Colombia grabs new powers</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>A dirty war gets dirtier</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 20, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH'S "war on terrorism" gets dirtier all the time. Last week, the government of Colombian President Alvaro Uribe--one of Bush's "anti-terror" allies--gave its police and security forces increased powers to detain, arrest and search "suspects" without any evidence or search warrants. The measures spell danger for anyone on the government's long list of "enemies."</P>
<P>"The civilian population has a lot to fear with these measures," said Gustavo Gallon, a respected human rights activist. "It is clear that this government sees suspects on every corner. It sees possible guerrilla collaborators in every human rights activist, union leader and even journalist. They are going to be detaining people on a whim."</P>
<P>Meanwhile, the U.S. announced plans to send instructors to train Colombian army units next month in the province of Arauca, where the Colombian army works with right-wing paramilitaries that use terror of the most brutal kind to silence all opposition. The homicide rate in this province is 160 per 100,000 people--compared to 64 per 100,000 in the rest of Colombia and six per 100,000 in the U.S. </P>
<P>Why has the U.S. focused on Arauca? For the same reason that the U.S. is threatening war against Iraq--oil. U.S.-based oil giant Occidental Petroleum has a 483-mile-long pipeline to take oil from Colombia's interior to the coast--oil that represents 5 percent of Occidental's revenue.</P>
<P>In Bush's world, terror against ordinary Colombians is far preferable to terror against oil profits.</P>

