<P><font face="Times New Roman, Times, serif" size="4"><b>INS is stepping up deportation drive</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>The racist Immigration and Naturalization Services (INS) is trying to deport Bronx rapper Slick Rick, who's been in detention since June 1. </P>
<P>Readers who follow music might know Rick for his distinctive black eye-patch. He was born in England and moved to the Bronx at the age of 11. In 1990, he was convicted of attempted murder and spent several years in prison. </P>
<P>The INS attempted to deport him first in 1995, but failed. In 1996, Congress passed a law allowing the INS to detain or deport noncitizen felons even after they had served their time. Rick has a wife and child, who are both U.S. citizens.</P>
<P>Slick Rick's fame, connections and fortune will probably spare him from being deported. But if a well-to-do legal immigrant can be detained for months on end by the INS, what does this say to the poor and undocumented? As he put it: "If I wasn't Slick Rick, there would be no sign of any sympathy. I'm not the only bad case. There are a hundred other cases that are even worse than this."</P>
<P>Rick isn't a saint--his albums often include sexist lyrics--but this is a question of human rights. The INS needs to be abolished and people given the same free-flow across the borders as multinational commerce is already afforded.</P>
<B><P>John Green,</B> Davis, Calif.</P>

