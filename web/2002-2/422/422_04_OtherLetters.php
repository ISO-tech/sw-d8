<p><a name="soldiers"></a><font face="Times New Roman, Times, serif" size="4"><b>When U.S. soldiers rebelled</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>A recent letter to the editor (<I>SW,</I> August 30) argued that the murders and murder-suicide by Fort Bragg troops happened because "these are soldiers who were taught to murder, maim and participate in a slaughter in Afghanistan." </P>
<P>"Is it any wonder that sooner or later they bring that violence home?" asked the author. "These murders are a product of the changes that a person undergoes when confronted by the horrors of war."</P>
<P>Readers could hardly be blamed for concluding from this line of argument that soldiers are nothing but a pack of mindless homicidal maniacs. But if that's true, then how can what soldiers did to end the war in Vietnam possibly be explained?</P>
<P>They were also ordered to murder and maim. Many followed those orders--until they rebelled. "By every conceivable indicator, our Army that now remains in Vietnam is in a state approaching collapse, with individual units avoiding or having refused combat, murdering their officers and non-commissioned officers, drug-ridden and dispirited where not near mutinous," wrote Col. Robert Heinl in the <I>Armed Forces Journal</I> in June 1971.</P>
<P>The revolt of the Tsarist troops in 1917 started the Russian Revolution, and the revolt of the American troops in Vietnam signaled the end of the war. </P>
<P>Some on the left, instead of scorning soldiers as disgusting homicidal maniacs, did everything they could to reach out to rank-and-file soldiers with political arguments against the war, and helped soldiers organize to oppose it.  </P>
<P>To understand why it is necessary for us to do the same today, read "The GI's Revolt" in <I>The American War: Vietnam,</I> by Jonathan Neale, available from Haymarket Books.</P>
<B><P>Thomas Barton,</B> New York City</P>

