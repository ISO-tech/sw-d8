<p><a name="bans"></a><font face="Times New Roman, Times, serif" size="4"><b>I support bans on smoking at work</b></font></P>
<B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>Sherry Wolf recently wrote a letter about smoking bans and taxes (<I>SW,</I> August 30), in which she said: "Unfortunately, many progressives support the smoking bans and higher taxes."</P>
<P>I agree that sales taxes are regressive and an attack on workers that should be opposed. However, I think <I>SW</I> should support smoking bans in workplaces--airplanes, restaurants, offices, factories, etc.</P>
<P>Environmental tobacco smoke (or "second-hand smoke") is a poison. It is classified as a Class-A carcinogen--as is asbestos--by the federal government. Its presence at work is a danger that people should not have to endure in order to make a living.</P>
<P>Flight attendants' unions have fought for and won smoking bans on both domestic and international flights. Just as <I>SW</I> supports other workplace-safety laws (and opposes the ongoing cutbacks in OSHA inspectors), so too should it support smoking bans.</P>
<B><P>Jason Yellen,</B> Boston</P>

