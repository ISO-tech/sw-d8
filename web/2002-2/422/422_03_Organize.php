<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Turn doubts about Bush's war drive into antiwar activity</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Time to organize</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 20, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH gang is determined to go to war on Iraq. But they'll have growing opposition to deal with.</P>
<P>Last year at this time, the White House was using the national and international outrage at the September 11 attacks to pave the way for the bombing of Afghanistan. A significant minority of people recognized this cynical exploitation of a tragedy for what it was--and turned out in impressive numbers to antiwar teach-ins, vigils and demonstrations. But once the bombs began dropping in early October, the antiwar opposition was increasingly swept to the margins.</P>
<P>The months that followed the U.S. "victory" in November were difficult ones for opponents of the U.S. war machine, with pro-war cheerleaders loudly demanding that our side admit we were wrong. But the tide began to shift this summer.</P>
<P>Today, every opinion poll shows significant opposition to Bush's war drive--including an outright majority opposed to the U.S. acting without the support of other countries.</P>
<P>One year after September 11, there is a much larger group of people who don't trust Washington's excuses. That means the potential for rebuilding an antiwar movement is much greater. It's time to get to work making it happen.</P>
<P>As in other wars, college campuses will be the most energetic section of the antiwar movement, at least initially. Building campus antiwar groups will be the crucial first step to mobilizing opposition--and turning doubts into action at the grassroots.</P>
<P>There will be many national antiwar initiatives to support and mobilize for. But the real test will be whether we can draw in new groups of people into local organizations, involving them in the task of rebuilding the movement from the bottom up.</P>
<P>Doing this will mean trying to attract a broad audience. The antiwar movement shouldn't set up obstacles for people whose ideas have only begun to change--for example, by insisting that opposition to a war on Iraq be linked to support for the Palestinian struggle.</P>
<P>Obviously, for socialists, the connection between the U.S. war drive and Israel's war on Palestinians is clear--and in fact, new activists will have to learn about the Palestinians' struggle for justice if they are to become consistent opponents of imperialism. But this discussion should take place over time--in groups that are open to anyone opposed to the U.S. war on Iraq.</P>
<P>In fact, political education and discussion will be crucial. There are countless questions that activists will have to answer in the course of this struggle--from what the Bush gang is after, to the U.S. attempt to get the United Nations (UN) to support a war. How activists answer these questions will determine whether we go forward or not.</P>
<P>For example, during the lead-up to the 1991 Gulf War, many activists insisted that the antiwar movement should call for the UN to take action of some sort against Iraq--rather than the U.S. These activists were disarmed when the UN, despite all the rhetoric about peace, did sanction a war run by Washington.</P>
<P>Likewise, the call to "let sanctions work" against Iraq also undermined the fight against Bush Sr.'s war drive by conceding that the U.S. had the right to wage an economic war. A decade later, sanctions have "worked"--by killing many more ordinary Iraqis, just as this newspaper and other opponents of the Gulf War always pointed out.</P>
<P>These and other questions will face today's antiwar movement. The movement will be stronger for having an open discussion of them. On campuses and in cities over the past several weeks, antiwar groups are coming together to take a stand.</P>
<P>It's time to organize.</P>

