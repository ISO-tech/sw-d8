<P><font face="Times New Roman, Times, serif" size="5"><b>Protest the IMF and World Bank</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Ben Dalbey</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 20, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WASHINGTON--Activists from up and down the East Coast and beyond will come to Washington on the weekend of September 28-29 to protest the annual meetings of the World Bank and International Monetary Fund (IMF). </P>
<P>The Mobilization for Global Justice (MGJ), the main coalition of groups and individuals organizing against the Bank and IMF in D.C., has called for a mass rally and march on the afternoon of September 28--followed by nonviolent civil disobedience in the evening to symbolically "quarantine" the delegates inside the meetings. There will also be teach-ins and other events in the run-up. Organizing for the protests was limited until recent weeks, when meetings got larger after students returned, and more groups have come on board.</P>
<P>While efforts within the MGJ to add an antiwar demand to the group's platform were blocked this summer, peace and antiwar groups are now holding a separate event on September 29 against Bush's coming war on Iraq. </P>
<P>This march can be a valuable opportunity to show how the IMF's policies of "structural adjustment"--which place the greed of Western banks and corporations over the needs of people in poor countries--are connected to the U.S. government's plan to be the world's super-cop.</P>
