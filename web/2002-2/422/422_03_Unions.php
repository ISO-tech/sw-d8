<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>When will unions fight back?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 20, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>EMPLOYERS ARE out to gut the strength of the some the most powerful unions in the U.S.--but labor leaders are surrendering without a fight.</P>
<P>Last week, a 62 percent majority of workers at Boeing Co.'s commercial jet aircraft division voted to reject a contract that would allow the loss of thousands more jobs--on top of 30,000 layoffs announced since the September 11 attacks last year. But because the rules of the International Association of Machinists (IAM) require a two-thirds vote to authorize a strike, the contract will go into force anyway.</P>
<P>The responsibility for this disaster rests with IAM officials, who did nothing to prevent their "partners" in Boeing management from violating job protections established in the previous two contracts--including those won after a 69-day strike in 1995.</P>
<P>This time around, IAM officials held one vote on a proposed contract--only to refuse to count the ballots while they appealed to management for further negotiations. This led to bitterness and distrust among the rank and file.</P>
<P>IAM leaders are also pushing concessions for their members at United Airlines, where management is demanding givebacks totaling $9 billion from employees over six years.</P>
<P>Meanwhile, another bulwark of the labor movement, the West Coast-based International Longshore and Warehouse Union (ILWU), has already conceded demands for the elimination about 1,000 clerks jobs--and kept dockworkers on the job after the contract expired July 1.</P>
<P>But the employers--backed by Bush administration threats to ban any strike on the grounds of "national security"--are demanding even more givebacks. ILWU leaders have denounced these threats. But they tried to claim the mantle of "terrorism prevention" for the union--as if that was the real issue for the union busters in the White House. When union militants questioned this strategy, they were hounded by officials.</P>
<P>Even the Teamsters contract at UPS, hailed as a "victory" by union leaders, allows management to roll back gains for part-time workers won in the groundbreaking 1997 strike.</P>
<P>Meanwhile, AFL-CIO President John Sweeney has ducked the debate around Bush's drive for a war on Iraq, used the September 11 anniversary to declare labor's patriotism and embraced the Democrats' mushy program for the 2002 elections.</P>
<P>Yet despite the paralysis at the top, the potential to take on Corporate America is there. A recent opinion poll for the AFL-CIO found that 58 percent of workers aged 18 to 34 would join a union if they could.</P>
<P>And in Chicago, some 7,000 members of the Hotel Employees and Restaurant Employees Local 1--most of them poorly paid immigrants--last month mobilized for a strike in a contract campaign that culminated with an electrifying march downtown.</P>
<P>Unfortunately, Local 1 leaders, under pressure from politicians and the local labor leaders, kept talking past the contract deadline and settled without a strike. The contract made dramatic gains, but a strike could have won more--and given a much-needed display of workers' power to stand up in a recession-wracked economy.</P>
<P>Union officials may be reluctant or unwilling to lead struggles. But rank-and-file workers have no alternative but to organize and fight back--and build unions that can take on the employers.</P>

