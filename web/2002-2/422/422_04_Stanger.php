<P><font face="Times New Roman, Times, serif" size="4">How a UPS Teamster fought an unjust firing</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Mobilizing saved my job</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 20, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>I appreciated Nate Moore's letter describing my firing from United Parcel Service (<I>SW, </I>July 26) and figured I'd update you. </P>
<P>When I was fired, we refused to go quietly into the grinding grievance process. Twenty Teamsters from my shift went with our steward to present my discharge grievance to management, and I used my time off to leaflet all over the state about the pending contract ratification vote. A labor-community alliance mobilized, getting petition signatures and organizing a week of "Harass UPS Back" phone calls during my shift.</P>
<P>Two days before my hearing, we sent a delegation of labor and community leaders to my workplace to deliver more petitions, with more than 600 signatures demanding my reinstatement with back pay. The driver union steward and the boss accepted the petitions, but refused to discuss my case. The steward told the community delegation to leave the property.</P>
<P>I was reinstated without back pay, after the New England grievance panel heard my case in Providence, R.I. The panel found that: "There was no just cause for the discharge of the grievant. The discharge shall be converted to a suspension from June 21st through August 16, 2002, providing clear notice to the grievant that failure to abide by work rules is a serious offense and can be subject to termination for a subsequent infraction."</P>
<P>There was no further explanation. The case didn't go to the neutral arbitrator who sits on the panel for discharges--because there was no "deadlock" between the company and union reps. Without "just cause" and under rules that change all the time and are only enforced for me, I was slapped with a seven-week suspension, will lose my health benefits and will be fired for another infraction. </P>
<P>I believe that the mobilizing we did here was the thing that got me back on the shop floor at all. Last week, I had a "reinstatement" meeting with management and my business agent. I was told to follow all rules and grieve if I had a problem: "Work now, grieve later." It was made clear that UPS is allowed to make any rule to hassle me. </P>
<P>Then last week, that same driver steward insinuated to me that leafleting could soon be restricted. Workers are subject to constant petty discipline, and are told that "because of Dawn, UPS has to document everything now."</P>
<P>Our pre-load steward recently filed a huge safety grievance about the workplace and discipline. Almost all the workers signed on, so pre-load activism continues, but until more Vermont Teamsters become more active within their union, activists will be vulnerable. </P>
<P>That is why we've scheduled an upcoming Teamsters for a Democratic Union (TDU) meeting. We need more informed, active Teamsters if we're to have decent working conditions here.</P>
<B><P>Dawn Stanger,</B> Teamsters Local 597, Vermont</P>

