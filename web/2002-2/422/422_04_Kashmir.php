<P><font face="Times New Roman, Times, serif" size="5"><b>Washington's hypocrisy about Kashmir</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 20, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>, </P>
</B><P>On September 12, the United Nations (UN) assembly will bring together leaders of various governments around the world. Among them will be the rulers of the two South Asian rivals, India and Pakistan.</P>
<P>In preparation for the summit, you would think that U.S. politicians would arrange for some sort of dialogue between the two leaders about the prospects for self-determination in the disputed Kashmir region. After all, these are the same politicians who waste no opportunity to preach to the world about "freedom" and "democracy."</P>
<P>Did the Bush administration issue a statement about the human rights abuses conducted by Indian military forces in Kashmir? Or perhaps a criticism of Pakistan's own denial of rights in the portion of Kashmir that it controls?</P>
<P>Neither. Instead, on the eve of the UN meeting, the U.S. Congress released a report that "warned" against an independent Kashmir. According to the Pakistani newspaper <I>Dawn,</I> the congressional report claims, "An independent Kashmir could become a serious threat to the world as a breeding ground for terrorism."</P>
<P>It went on to say that an independent Kashmir would become "another permanent sanctuary for Islamic extremist terrorist operations." This lets the real terrorists in Kashmir off the hook--the Indian military forces that have put down a decade-long independence struggle with brute force. Some 60-80,000 people have died as a result.</P>
<P>The Indian government was exultant, according to press reports. No doubt Pakistan's "President General" Musharraf was pleased as well. For all their talk of self-determination, Pakistan's rulers don't want to see an independent Kashmir either.</P>
<P>For the Kashmiris themselves, this comes as yet another confirmation that they cannot rely on the goodwill of politicians in the West, but need to continue their struggle for freedom.</P>
<B><P>Ganesh Lal, </B>Greensboro, N.C.</P>

