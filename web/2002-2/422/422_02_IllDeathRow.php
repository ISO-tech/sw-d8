<P><font face="Times New Roman, Times, serif" size="4">Illinois prisoners' hunger strike protests conditions</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Driven to suicide on death row</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Joan Parkin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 20, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>DEATH ROW prisoners at the Pontiac Correctional Center in Illinois went on hunger strike earlier this month to protest the official inaction that led to the suicide of a fellow prisoner.</P>
<P>Frank Redd, a diagnosed schizophrenic, hung himself in his cell September 6. Two weeks earlier, prison officials had placed Frank in an observation cell--naked--after he tried to hang himself. After 72 hours, they returned him to his cell--without counseling or treatment, other prisoners say.</P>
<P>On the day that he died, Frank screamed for hours, according to fellow prisoner David Harris--saying that "they're coming to get us" and that his family would come to claim his body. Yet when other prisoners got a guard to check on Frank, the guard's response was, "There's nothing wrong with you," Harris said.</P>
<P>Frank Redd may have taken his own life, but Pontiac officials drove him to it. Over the past two years, law and art classes for prisoners have been suspended, personal paintings were removed from cells, prisoners are shackled and chained to their seats during visits, and they must put all personal belongings in a black box whenever they leave their cells--even to take a 10-minute shower.</P>
<P>For a mentally disabled man like Frank--whose cell was covered with his own paintings before officials put in the new rules--it was too much. "This place does something to the mind," Renaldo Hudson, whose cell was next to Frank's, wrote in a letter. "I was next door to the brother, I hope someone remembers my friend." </P>
<P>Frank denied being involved in the murders that he was convicted of. Without physical evidence, Chicago police detectives from Area 3 headquarters tried to get Frank to confess--by beating him with a telephone book, Frank's sister Gloria said. When that failed, they threatened Gloria with losing her 6-year-old son until she made a statement implicating Frank. Gloria retracted that statement in court, but prosecutors still won their conviction.</P>
<P>After his suicide, more than 40 death row prisoners participated in the hunger strike, which lasted for several days. Members of the Campaign to End the Death Penalty organized a press conference to call attention to the suicide and the hunger strike.</P>
<P>Frank's death should send up a red flag about conditions on death row--especially considering the fact that Gov. George Ryan is considering a blanket commutation of all death sentences in Illinois.</P>
<P>As exonerated former death row prisoner and Campaign member Darby Tillis put it, "How horrendous and gruesome death row is that, even on the eve of the commutations, a man will take his own life."</P>

