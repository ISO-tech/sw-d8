<P><font face="Times New Roman, Times, serif" size="4">Ashcroft and the Feds shred civil liberties</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>An attack on all our rights</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 20, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE ASSAULT on civil liberties is ratcheting up as politicians and prosecutors use the "war on terror" to further their agenda of curtailing all our rights.</P>
<P>When Attorney General John Ashcroft and the FBI trumpeted the arrests of a supposed al-Qaeda "sleeper cell" in upstate New York last week, the mainstream media once again found the six men guilty until proven innocent, on the flimsiest of evidence. But at the same time, one of the government's high-profile cases was being exposed as a witch-hunt. </P>
<P>As <I>Socialist Worker</I> went to press, federal prosecutors were trying to keep Enaam Arnaout, the head of a Chicago-based Islamic charity, behind bars in solitary confinement--after a judge threw out perjury charges against him on September 13.</P>
<P>Arnaout and the Benevolence International Foundation (BIF) were first targeted in December, when FBI agents raided the charity's offices and Arnaout's home--and froze BIF's assets because of its supposed ties to "terrorism."</P>
<P>In fact, Arnaout had asked the government to inspect BIF's records to remove any suspicions--before the raid. And although he was abroad at the time of the raid, he returned to the U.S. voluntarily.</P>
<P>When BIF sued the government to win the right to resume normal operations, the Feds arrested Arnaout April 30--on perjury charges for denying any BIF connection to terrorist or military organizations. Their evidence? Old photos that supposedly showed Arnaout in the same location in Bosnia as Osama bin Laden--though not together.</P>
<P>The real message was that because Arnaout stood up to the government, they would make an example of him.</P>
<P>Last week, Judge Joan Gottschall ruled that prosecutors couldn't charge Arnaout with perjury. But within hours, prosecutors had filed new charges in a bid to keep him behind bars.</P>
<P>This travesty is only one of thousands since the Bush administration used September 11 to push the USA PATRIOT Act through Congress. By official admissions, some 1,200 people--almost all of them of Arab or Muslim background--were detained in the September 11 investigation, more than half on immigration violations unrelated to terrorism. </P>
<P>The government claims that just over 150 remain behind bars. But there has been no independent verification--or disclosure of any of the names of people who were rounded up.</P>
<P>At the same time, the government is holding 600 "enemy combatants" in a prison camp at the U.S. military base in Guant&aacute;namo Bay, Cuba--and is insisting on its right to keep the prisoners indefinitely.</P>
<P>"[O]ver the last year, the U.S. government has taken a series of actions that have gradually eroded basic human rights protections in the United States, fundamental guarantees that have been central to the U.S. constitutional system for more than 200 years," says the Lawyers Committee for Human Rights.</P>
<P>Government officials justify all this in the name of fighting terrorism, but the likes of Ashcroft have been plotting an assault on our rights for years. "The Justice Department had unsuccessfully sought many of the proposals well before September 11 to bolster routine drug cases and other non-terrorism investigations," the American Civil Liberties Union pointed out in a recent report.</P>
<P>The shameful results of the Bush gang's national hysteria about Arabs and Muslims were seen last week in Florida, when a waitress in a restaurant reported that she overheard three Arab men plotting a bombing.</P>
<P>A roadside search and a 17-hour detention revealed that the three men--all medical students--were innocent. But the three were told not to come back to Larkin Community Hospital in Miami, where they were training. "He wasn't convicted of anything, and we're in America," said Ayman Gheith's brother, Abdallah. "What happened to the Constitution?"</P>
<P>This assault on democratic rights goes far beyond scapegoating Arabs and Muslims. For example, Bush wants to use the excuse of "national security" to deny union and civil service protection to tens of thousands of workers in the new Department of Homeland Security.</P>
<P>It's time to expose the Bush-Ashcroft crackdown for what it is--a shameful attack on all of our rights.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Ashcroft's power grab</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>UNDER USA PATRIOT and other new measures, the federal government has claimed the right to:</P>
<P>--Search private homes without notice or probable cause</P>
<P>--Access the financial records of any U.S. citizen</P>
<P>--Spy on any public political or religious gathering</P>
<P>--Use the CIA for domestic investigations</P>
<P>--Hold noncitizen detainees indefinitely in secret and without trial </P>
<P>--Hold secret hearings on the status of immigrants and detainees</P>
<P>--Deny jailed suspects the right to confront their accusers</P>
<P>--Monitor private discussions between lawyers and their clients </P>
<P>--Prosecute librarians for disclosing that federal agents sought material in connection with terrorism investigations</P>

