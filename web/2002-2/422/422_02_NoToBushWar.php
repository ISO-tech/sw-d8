<P><font face="Times New Roman, Times, serif" size="4">Say no to Bush's war on Iraq</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Growing numbers question war drive</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte and Josh Brand</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 20, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AS THE Bush administration drums up support for its attack on Iraq, growing numbers of people are coming together to question the war drive--and show their opposition.</P>
<P>On September 10, more than 1,000 people turned out at George Mason University near Washington, D.C., to hear Robert Fisk, Middle East correspondent for Britain's <I>Independent</I> newspaper, deliver a withering barrage against a U.S. war in Iraq. </P>
<P>Fisk exposed George W. Bush's real aim--increasing U.S. power in the Middle East and around the world. And he saved his harshest words for the mainstream media, which used Pentagon press releases to craft stories about the U.S. war on Afghanistan.</P>
<P>The huge turnout showed the thirst for real answers about the Bush war machine.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<P>On the anniversary of the September 11 attacks, people around the country registered their anger at Washington's attempts to turn last year's tragedy into a call for war.</P>
<P>In New York City, 3,000 people came to the first three hours of an all-night vigil September 10 in Washington Square Park. Speakers included members of September 11 Families for Peaceful Tomorrows, author Manning Marable, Michael Ratner of the Center for Constitutional Rights, Martin Luther King III, and "Democracy Now" radio show host Amy Goodman. With New York awash in patriotism, the vigil brought together thousands of people looking for an alternative.</P>
<P>In San Francisco, 1,500 people turned out for a concert and peace vigil on September 11. "The emotions we felt were manipulated by people in Washington to support the so-called war on terrorism," left-wing writer Robert Jensen told 200 protesters at the University of Texas. "It's not really a war on terror; it's a war on the rest of the world."</P>
<P>Protests, large and small, are taking shape as Bush pushes for a war on Iraq. As Bush made his case at the United Nations (UN) on September 12, about 200 antiwar activists chanted "No blood for oil!" outside. Police went to great lengths to discourage protesters, setting up barricades and requiring protesters to use a "password" to enter the demonstration.</P>
<P>A few days later, 1,000 New Yorkers marched to denounce Bush's plans for slaughter in Iraq. In San Francisco, 1,000 attended a "Don't attack Iraq!" march to UN Plaza.</P>
<P>In Rochester, N.Y., about 150 protested at the Federal Building, and some 40 people demonstrated at the INS Detention Facility in Batavia, where detainees from John Ashcroft's sweeps have been held.</P>
<P>And organizing has begun at many schools, such as the University of Maryland where students are organizing information tables to oppose the war.</P>
