<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>What they mean by regime change</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 20, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WITHIN DAYS of last September 11, Deputy Defense Secretary Paul Wolfowitz casually announced that the Bush administration would seek to "end states" that harbor terrorists. Although the administration slapped down Wolfowitz for his candor, his plan has become official U.S. policy. </P>
<P>Bush speaks of "regime change" in Iraq and Palestine only months after overthrowing the Taliban regime in favor of a puppet government in Afghanistan. If Bush is successful in overthrowing the Iraqi government, he won't stop there. Administration hawks have mentioned Iran, Saudi Arabia, North Korea, and Syria, among others, as other candidates for "regime change"--even if they have nothing to do with terrorism or the September 11 attacks.</P>
<P>There's nothing new about the U.S. overthrowing "unfriendly" governments. The CIA installed the Shah of Iran in power in 1953, and the U.S. helped organize the military coup that overthrew Chilean President Salvador Allende in 1973. Presidents Reagan and Bush I ordered invasions of Grenada and Panama that replaced the existing governments with pro-U.S. regimes.</P>
<P>Only its brazenness and its willingness to issue death threats to governments by fiat sets the "Bush doctrine" apart from these earlier escapades.</P>
<P>Throughout the summer pseudo-debate involving the Bush II hawks and establishment figures from the Bush I administration, no one has questioned whether the U.S. has the right to demand "regime change" in Iraq.</P>
<P>The Bush II hawks have tried to sell their invasion as a way to "liberate" Iraqi people. "There will be dancing in the streets" after the U.S. takes care of Saddam, says Defense Policy Board Chair Richard Perle. If so, the dancing won't last long, because the regime the U.S. installs will owe its existence to the U.S.--not to the Iraqi people.</P>
<P>In 1963, the CIA engineered the overthrow and assassination of Ngo Dinh Diem in South Vietnam. Diem's crime was to vacillate when the U.S. demanded expansion of the Vietnam War. The U.S. replaced Diem with a series of military juntas entirely dependent on the U.S., but committed to the war.</P>
<P>The Vietnamese people never accepted the South Vietnamese puppet regimes. Twelve years after Diem's murder, and at great human cost, the Vietnamese finally won their independence when they drove out the U.S. The Vietnam War was a victory for self-determination of the Vietnamese people.</P>
<P>Self-determination isn't the U.S. plan for Iraq, as Bush's father confirmed in 1991. In 1991, Papa Bush called Saddam to "step aside," a phrase widely interpreted as U.S. support for efforts to overthrow Saddam's regime.</P>
<P>For a few weeks in March, uprisings in Iraqi Kurdistan and the Shia south had Iraq's regime on the run. But the U.S. feared civil war and a breakdown of the country, so it allowed Saddam's military to suppress the revolt.</P>
<P>Bush I had meant to signal Iraqi military officers to depose Saddam. Then-National Security Adviser Brent Scowcroft later said the U.S. aimed for an "iron-fisted" military regime.</P>
<P>The last thing the Bush administration wanted was the example of a successful revolution toppling a Middle East dictatorship. A real democracy might drive a harder bargain with the U.S. for access to oil. The Bush II hawks envision a succession of wars to enforce their will on the world.</P>
<P>The U.S. has no right to determine the future of Iraq, Palestine or anywhere else. Only the people of those countries can determine their futures--like the Vietnamese did.</P>
<P>Ordinary Americans have nothing to gain from endless wars of conquest. We have to exercise our own self-determination--to organize an antiwar movement to prevent them.</P>
<P>By 1968, the Vietnam antiwar movement grew strong enough to force President Johnson out of office. That's the kind of "regime change" we should endorse today.</P>

