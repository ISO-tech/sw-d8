<P><font face="Times New Roman, Times, serif" size="5"><b>Florida vote fiasco&#133;again</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 20, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>TWO YEARS after George W. Bush stole the presidential election there, the Florida state government showed yet again last week that it can't figure out how to organize a democratic election.</P>
<P>Voting was a disaster in primary elections September 10, which left the most important race--the vote for the Democratic Party's nominee for governor, between former Attorney General Janet Reno and lawyer Bill McBride--up in the air a week after the vote. As in 2000, most of the allegations that votes weren't counted or voters were turned away came from mainly African American areas.</P>
<P>After the 2000 debacle, Gov. Jeb Bush promised to reform the state's election procedures and modernize equipment. Florida counties spent millions on new voting machines. But the new system failed its first significant test.</P>
<P>Thousands of voters were turned away when electronic voting machines failed to start up. In some precincts, machines recorded no votes, and in one precinct, the computers reported a 900 percent turnout.</P>
<P>Jeb Bush and his campaign advisers worried that the fiasco would rekindle memories of how voter disenfranchisement and election-day screw-ups helped big brother George steal the White House. So Jeb turned to the same tactic as in 2000--blaming Democrats. "What is it with Democrats having a hard time voting--I don't know," he sneered.</P>
<P>What a hypocrite! This second Florida fiasco only underlines how little the powers that be care about the right to vote. Neither Republican state officials nor Democratic Party appointees in charge of voting locally took the steps necessary to make sure that Florida's elections worked.</P>

