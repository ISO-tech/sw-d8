<P><font face="Times New Roman, Times, serif" size="5"><b>Hidden crisis in health care</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 20, 2002 | Page 5</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>JUST A few years ago, with cases of HMO corruption making headlines, the politicians promised an angry public that they would make health care reform a top priority. Today, they're quibbling over how limited they can make prescription drug benefit for Medicare recipients--and they can't even agree on that.</P>
<P>What none of the politicians dares to talk about is the hidden crisis in U.S. health care--with a majority of patients paying more money for fewer services at hospitals, while health care workers struggle to provide care at dangerously low staffing levels. ELIZABETH SCHULTE looks at the health care disaster.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<P>HOSPITALS ARE supposed to make sick people well. But according to the Centers for Disease Control and Prevention, infections linked to hospital germs are the fourth leading cause of death among Americans. </P>
<P>Infections contracted in a hospital kill more people each year than car accidents, fires and drowning put together. According to a <I>Chicago Tribune</I> investigation, in 2000, nearly three-quarters of these deadly infections were the result of unsanitary conditions.</P>
<P>The <I>Tribune</I>'s examination of government and hospital industry reports revealed that more than 75 percent of all hospitals have been cited for significant cleanliness and sanitation violations since 1995--but over the same period, hospitals have collectively cut back cleaning staff by 25 percent.</P>
<P>Nurses, too, have felt the budget ax, as hospitals try to "cut costs." So it should be no surprise when the quality care that they were trained to provide takes a back seat. "When you have less time to save lives, do you take the 30 seconds to wash your hands?" Trande Phillips, a San Francisco nurse, told the <I>Tribune</I>. "When you're speeding up, you have to cut corners. We don't always wash our hands. I'm not saying it's right, but you've got to deal with reality."</P>
<P>At the same time as fewer people have access to decent health care, what workers pay for this care has gone through the roof. According to a survey released this month by the Henry J. Kaiser Family Foundation, health insurance premiums have increased six years running--and by ever-larger amounts, jumping last year by 12.7 percent.</P>
<P>More than half of large firms forced their employees to pay more for health insurance this year, according to the survey, and more than three-quarters expect to raise costs next year. Even after their employers' contribution, U.S. families now spend an average of $174 a month for coverage--up from $150 last year.</P>
<P>Other workers have lost coverage completely. The Kaiser study found that only 61 percent of small businesses offered health plans in 2001, down from 67 percent the year before.</P>
<P>The insurance giants that charge more than ever for coverage are taking their lead from drug companies and the huge for-profit hospital chains such as Hospital Corporation of America. Even as the economy suffers through a recession, it has been a red-letter year for the big for-profit hospital chains.</P>
<P>Some chains, like Triad Hospitals and Tenet Healthcare, are taking advantage of the fact that many nonprofit hospitals are feeling the effects of drastic cuts in federal and state funding--and are gobbling them up.</P>
<P>"You buy it, you fix it, you grow it, and you do it again," was the strategy described by Sheryl Skolnick, managing director at the investment research firm Fulcrum Global Partners. "Fix it," of course, means cuts in staff and services--and making the sick pay.</P>
<P>Skolnick also applauded Tenet for what the <I>New York Times</I> described as the company's "ability to get cold, hard cash by collecting from patients when they are admitted." Tenet took in almost $14 billion last year.</P>
<P>Meanwhile, in order to trim $31.3 million from next year's budget, Los Angeles County is closing 11 health centers and four school-based health clinics by October 1. The county also plans to trim the budget by reducing childhood immunizations and screenings for sexually transmitted diseases.</P>
<P>Anna Garcia, a sixth-grader at an elementary school in Pacoima, where the county plans to shut down a health clinic, was among 700 people who came to protest a county supervisors' meeting in August. "We live in Pacoima, and our families are poor, but children do not have to die because they are poor," Garcia said.</P>
<P>This is the shameful reality of a health care system that puts profits before saving people's lives. We deserve better.</P>

