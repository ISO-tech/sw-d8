<P><font face="Times New Roman, Times, serif" size="5"><b>Unionists organize antiwar conference</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Thomas Barton, trustee, AFSCME Local 768</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 20, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Union members opposed to the U.S. war on Iraq will meet here in a one-day conference October 19. The conference, called by New York City Labor Against War (NYCLAW), is aimed at a local audience, but labor antiwar activists from around the U.S. are also expected to attend.</P>
<P>"We are trying to deepen and extend antiwar activity within labor, especially against the war that the government is ramping up against Iraq," said Michael Letwin, president of Legal Aid Attorneys/UAW Local 2325 and a co-convenor of NYCLAW. "I think that over time there has been more and more openness to antiwar activity, including in labor, and I think a war in Iraq will open that up in a big way."</P>
<P>The meeting will open with discussions on opposing the attack on Iraq; assaults on labor, civil and immigrant rights; and working-class antiwar movements in U.S. history. Moving to practical organizing, workshops will follow on what's been learned from experiences of antiwar unionists, immigrant rights activists and community organizers during the past year. The closing workshops are to develop strategies in specific unions and workplaces. The conference will also discuss what kind of structure NYCLAW should have for the coming year.</P>
<P>NYCLAW was originally formed shortly after September 11 as union members' committee inside a broader citywide antiwar coalition. The group's statement notes that "the United States and its allies have already inflicted widespread suffering on innocent people in such places as Iraq, Sudan, Israel and the Occupied Territories, the former Yugoslavia and Latin America."</P>
<P>By April 2002, NYCLAW members had gathered endorsements against the war from 619 rank-and-file union members, elected officers and union bodies in the greater New York City area, and 393 from other cities and countries. That month, NYCLAW helped organize buses to the mass demonstration against war and for Palestinian rights in Washington, D.C. NYCLAW has also organized labor protests against the internment of immigrants at the Brooklyn House of Detention.</P>
<P>Along with Letwin, NYCLAW's co-convenors are Larry Adams, National Postal Mail Handlers Union Local 300; Ray Laforest, Staff Representative, DC 1707, AFSCME; and Brenda Stokely, president of AFSCME Local 215, in DC 1707.</P>
<P>All antiwar union members are invited to attend.</P>
<I><P>The conference will take place at AFSCME Local 1707, 75 Varick St., New York City. For more information, visit http://groups.yahoo.com/group/laboragainstwar or write to NYCLAW, Prince St. Station, P.O. Box 233, New York, NY 10012-3200.</P>
</I>

