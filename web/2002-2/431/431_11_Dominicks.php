<P><font face="Times New Roman, Times, serif" size="4">Chicago grocery workers under heavy pressure to take cuts</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Stand up to Dominick's!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 22, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--Almost 9,000 workers at 113 Dominick's grocery stores--owned by Safeway, Inc.--were working without a contract as <I>Socialist Worker</I> went to press.</P>
<P>Union members have been working without a contract since November 9. On November 10, more than 6,000 members of United Food and Commercial Workers (UFCW) Locals 881 and 1546 turned out to approve a strike authorization vote by more than 80 percent.</P>
<P>Under management's proposed four-year deal, the company would cut health care coverage altogether for some workers, pay less than half the cost of coverage for new employees, cut starting pay and shift the majority of benefit cost increases onto current employees.</P>
<P>Management also wants to eliminate contract language that allows higher-seniority, higher-paid workers first access to available work hours, which would create a "two-tier" system for union employees.</P>
<P>In the run-up to the vote, workers suffered intimidation and harassment--including being subjected to physical searches at closed-door mandatory meetings with management. </P>
<P>Unfortunately, some workers have bought the company's line. Management was able to organize a small group of workers to hold a rally calling for a new authorization vote--which the union rightly rejected. And some meat cutters and deli workers--who are covered under a separate contract--staged a small rally last week urging the union to accept the latest contract offer.</P>
<P>But if Locals 881 and 1546 are forced to accept a contract full of givebacks, you can bet that the meat cutters and deli workers--whose contract expires in eight months--will be next on Safeway's chopping block.</P>
<P>If any grocery store can afford to pay its workers a decent wage, it's Safeway, Inc. With more than 1,700 stores across North America, Safeway is the most profitable operator in the industry. Over the last decade, profits have tripled--outpacing the performance of Wal-Mart, the antiunion superstore chain that Safeway bosses regularly complain is cutting into their customer base.</P>
<P>"Here we are, they're telling us that they can't compete with Wal-Mart, well, then organize the workers at Wal-Mart--bring them up to our standards. Why should we lower our standards?" Local 881 shop steward Edgar Pacheco told <I>Socialist Worker</I>. As UFCW Local 881 President Ron Powell told reporters, "If they crack Chicago, this deal will be introduced everywhere."</P>
<P>Safeway's most vicious scare tactic has been the company's threat to shut down all of the stores in the event of a strike--and then reopen them under the Safeway name, effectively locking union workers out.</P>
<P>The company's sole purpose is to force workers to agree to a rotten deal and weaken the power of the union. That's why, for example, the company's last offer included a "buyout" clause--where the company would pay staff with two or more years of experience an estimated $1,000 per year of employment, up to $30,000, to quit. Safeway wants experienced staff to take buyouts so new employees can be hired at lower wages.</P>
<P>So far, the UFCW has been reluctant to call for a strike--opting for continued talks with Safeway management. Beginning November 19, UFCW members will hold informational pickets in front of stores during their time off to urge customers to tell Safeway to sign a fair contract--or sell the stores to a buyer that will. And if a deal on a new contract can't be reached by Thanksgiving, the union says it will strike.</P>
<P>Safeway is out to bust the union and steal workers' futures--and it will be up to the rank-and-file members of UFCW to make sure they don't get away with it.</P>

