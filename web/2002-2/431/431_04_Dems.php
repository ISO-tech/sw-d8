<P><font face="Times New Roman, Times, serif" size="4">Does it matter if the Democrats get a backbone?</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The confidence to fight back</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 22, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Since the Republican takeover in the midterm elections, there have been a lot of questions about how to make sense of it all. In particular, people have looked at how the Democrats threw the election away--and have drawn the conclusion that it doesn't matter if the liberal wing of party takes back the spotlight.</P>
<P>I think it is right to expose the Democrats for their real record and to say that the only way we will win on the issues we care about is through our own struggle. At the same time, I think it is shortsighted to say that it doesn't matter what liberals say on key issues like the war.</P>
<P>The November 8 issue of <I>Socialist Worker</I> was right to stress that "it would be welcome" if a liberal voice of opposition spoke up. When liberal groups and politicians stiffen up their backbone, it gives more confidence to people to mobilize for themselves.</P>
<P>Just look at the "Republican Revolution" fiasco of the 1994 midterm elections. Isolated protests took place all across the country to denounce House Speaker Newt Gingrich and his crew. But another key event took place, too. The National Organization for Women (NOW) had been planning a somewhat modest abortion rights rally in front of Congress to present a campaign they had been working on for some time.</P>
<P>The fact that this liberal women's rights group stood up for something against Gingrich touched off a firestorm. Instead of a modest rally, some 250,000 people showed up in April 1995--not just to fight for abortion rights, but to denounce all of Gingrich's Contract on America. </P>
<P>Neither NOW nor the Democrats who spoke at that demonstration had any notion of using that anger to build a movement. But because they put out the call, hundreds of thousands of people felt more confident to come out and fight. And socialists were there to participate and to argue what it will really take to win. </P>
<P>If some Democrats or groups like NOW finally get the nerve to stand up and denounce the Republicans for what they are, it'll be a good thing. It will also be up to socialists to be there with the people who answer those calls, to make the case that the Democrats might "talk the talk," but it will be up to ordinary people to learn to fight back for themselves.</P>
<B><P>Jeff Bale,</B> Washington, D.C.</P>

