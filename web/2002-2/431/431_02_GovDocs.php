<P><font face="Times New Roman, Times, serif" size="5"><b>Government documents down the memory hole</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 22, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IT'S THE "now you see it, now you don't" method for archiving federal government information.</P>
<P>Currently, documents and reports from all agencies in the executive branch of government are managed by a neutral body--the Government Printing Office (GPO). From there, librarians make the documents available to the public, online and in 1,300 public reading rooms around the country.</P>
<P>The system dates back to the 1800s--and is based on the idea that the American public ought to have access to the documents produced by "their" government.</P>
<P>But the Bush administration has new plans. The White House wants to transfer management of documents away from the GPO and into the hands of the agencies themselves. The information would go into a common computer database.</P>
<P>All that would be needed for a worried agency to delete information for good would be a few keystrokes--and embarrassing or possible incriminating documents could be simply "disappeared." As one magazine columnist put it, the administration proposal "threatens to gut federal document dissemination--and fast."</P>

