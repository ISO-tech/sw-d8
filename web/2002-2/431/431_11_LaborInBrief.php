<p><a name="Coca"></a><font face="Times New Roman, Times, serif" size="4"><b>Coca-Cola Downey Sales Center</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Evan Kornfeld</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>DOWNEY, Calif.--On November 8, merchandisers at Coca-Cola's Downey Sales Center voted to join the Teamsters union. This was a big victory for the Teamsters, who have been trying to organize Coca-Cola's merchandisers for years.</P>
<P>Merchandisers stock supermarket shelves with company products. This past summer, a unionization drive at the Rancho Cucamonga Sales Center failed, even though there was a class-action lawsuit from several current and former employees claiming the company retaliated against employees who complained about not receiving overtime pay.</P>
<P>The merchandisers at Downey may have received encouragement from the bottlers in the Downey production plant. Last summer, the bottlers--members of Teamsters Local 848--went on a one-day strike after voting down a proposed contract recommended by the union leadership, forcing the company to agree to a contract that workers found more favorable.</P>

