<p><a name="Fairbanks"></a><font face="Times New Roman, Times, serif" size="4"><b>Fairbanks Scales</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Erik Wallenberg</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ST. JOHNSBURY, Vt.--Police sprayed mace into a crowd of striking employees on a November 12 picket line at Fairbanks Scales.</P>
<P>On November 4, all but six of the 75 workers voted to strike. The workers--who are members of United Electrical Local 234--walked out in response to management's demands for a wage freeze, a $3,000 increase in annual health care costs and a freeze in the pension plan for the next year.</P>
<P>The strikers have set up a strong picket line with their supporters to shut down the plant and stop scab labor from taking their jobs. The police sprayed mace into the crowd to break into the line and allow a delivery truck through. In the chaos, one woman says, an officer hit her in the face.</P>
<P>In response, Caledonia County Sheriff Mike Bergeron said he hoped the strikers understood that his department has a "neutral position" in the dispute and that they were only there to keep the peace and maintain safety.</P>
<P>But this brutal response by the police to protect the company is a perfect example of how the police really aren't neutral. Throughout the day, police physically disrupted the picket line so that delivery trucks and scabs could freely enter and exit.</P>
<P>These strikers have been in the forefront of the fight for a living wage standard and a universal health care plan in the state. This attack is about undermining a strong and militant union. Continuing to organize solidarity actions with other unions and community supporters will be essential for victory.</P>

