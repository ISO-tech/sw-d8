<P><font face="Times New Roman, Times, serif" size="4"><b>Verizon</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By CWA shop stewards</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Verizon Communications has declared an all-out war on its unionized workers. Despite $2.1 billion in profits last quarter and praise from Wall Street, the phone giant is crying poverty and wants to lay off thousands in the Northeast.</P>
<P>In New York, Verizon could move to lay off roughly 3,000 union members represented by the Communications Workers of America (CWA) before Christmas. CWA has launched a $1.1 million publicity campaign to turn public opinion against Verizon.</P>
<P>Local politicians have been taken to see the deteriorating phone infrastructure and union members have leafleted neighborhoods, encouraging customers to complain about poor service to the Public Service Commission.</P>
<P>On November 14, more than 500 members of CWA and the other main union at Verizon, the International Brotherhood of Electrical Workers (IBEW) rallied in Boston to protest Verizon's plan to lay off 1,600 workers across New England by December 31.</P>
<P>Another rally with the two unions is set for November 21 at Ground Zero in New York City. CWA leaders hope this will create enough pressure to win rulings against layoffs in arbitration.</P>
<P>If the union wins the arbitration, the company would have to rehire laid-off workers with back pay. However, counting on arbitrators and the courts would be a mistake. In early November, the courts failed to grant CWA an injunction against layoffs until arbitration has been decided. Verizon can now lay off workers and then drag out the arbitration hearings--possibly until the contract expires in August 2003.</P>
<P>That's why union members must organize their workplaces for action--including a strike. Both CWA and the IBEW have committed to a mobilization plan, but follow-through has been weak. Every gang or work group needs to take the initiative--using buttons and petitions to organize. Stewards should argue for more backing from their locals.</P>
<P>If we strike, Verizon will try to get injunctions to end it and threaten us with fines and scabs. But we can beat Verizon with a strike and solidarity from labor. CWA won strikes at the company in 1998 and 2000 as well as in 1971 and 1989.</P>
<P>Verizon can survive bad publicity, but not a loss of dial tone.</P>
<I><P>Steve Trussel and Annie Levin contributed to this report.</P></I>

