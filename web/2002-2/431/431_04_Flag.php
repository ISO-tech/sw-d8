<P><font face="Times New Roman, Times, serif" size="5"><b>Should antiwar activists reclaim the flag?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 22, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>I must disagree with Elizabeth Schulte's position that antiwar demonstrators should repudiate the American flag (<I>SW,</I> November 8).</P>
<P>I completely agree with her recitation of the horrors committed in the flag's name. If the word "evil" has meaning, it could certainly be applied to the U.S. government--and if there is an "axis of evil," the U.S. government is at its center.</P>
<P>The problem, of course, is that most Americans don't see it that way. People consider themselves patriotic Americans who disagree with this or that government policy. At least around New York City, the flag has become, as a result of September 11, a general feel-good symbol. Many people I know lost friends or relatives that day and view flying the flag as a tribute to them.</P>
<P>I am personally made uneasy by flag waving and flag flying, but what I think isn't the point. The purpose of a demonstration is to attract as much support as possible. As long as Americans view their flag positively we should embrace it. Antiwar demonstrations in the U.S. have a primarily U.S. audience. They should be designed to attract U.S. support.</P>
<P>I would hope that we could educate people in the long run to see the horrors the U.S. government has perpetrated. But let's not make repudiation of the flag a prerequisite for joining an American antiwar movement.</P>
<B><P>Joe Grossman,</B> Suffern, N.Y.</P>

