<P><font face="Times New Roman, Times, serif" size="5"><b>Return of voodoo economics</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 22, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>HIS FATHER called it "voodoo economics." But that didn't stop the idiot son from spouting discredited gibberish in defense of last year's tax cut giveaway to the superrich.</P>
<P>After a meeting with his Cabinet last week, George W. Bush was asked by a reporter about the ballooning federal budget deficit. "The deficit would have been bigger without the tax-relief package," he said.</P>
<P>That's the same old "supply-side economics" lie that Ronald Reagan made famous 20 years ago--the claim that tax cuts for businesses and the wealthy stimulate enough economic activity to offset the drop in the amount of money the government takes in.</P>
<P>But no one but a few diehards actually believes it anymore. "I don't know anyone who has said that the makeup in revenue because of the economic effect is greater than the reduction," said Eric Engen, a former Federal Reserve economist with the conservative American Enterprise Institute.</P>
<P>Maybe Bush Jr. needs to phone home and talk to his father--who dismissed Reagan's supply-side lunacy as "voodoo economics" in 1980.</P>

