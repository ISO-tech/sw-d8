<p><font face="Arial, Helvetica, sans-serif" size="2">November 22, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>I agree totally with your article on Marriott workers (<I>SW,</I> August 30), as I am also a dislocated Marriott employee from one of the distribution centers that they have just closed.</P>
<P>They did nothing for their employees. I cannot see how this corporation was voted one of the best companies to work for--or how they got the other awards that they have received as an employer. </P>
<P>The public needs to know the real side of Marriott, but the company has managed to keep it quiet. They do not care about their employees and they should be ashamed of what they are doing to the employees of the World Trade Center.</P>
<P>It's something the public needs to know about.</P>
<B><P>A former Marriott employee</P>

