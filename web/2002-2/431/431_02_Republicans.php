<P><font face="Times New Roman, Times, serif" size="4">Bush uses election to demand wish list from Congress</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Republicans on the rampage</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 22, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE NEW Congress, where Republicans control both the House and Senate, won't take office until January. But George W. Bush's party is already on a rampage. The White House is using the Republican election victory as an excuse to push for legislation that has been deadlocked.</P>
<P>First on the wish list is a new Department of Homeland Security. The main point of the legislation is the elimination of union rights and civil service protections for the department's 170,000 workers--a bold slap at organized labor. But the version of the legislation passed by the House last week included slew of other add-ons to reward various corporate interests.</P>
<P>One provision, for example, would exempt drug companies from lawsuits stemming from any component or ingredient used in a vaccine--which could wipe out even currently pending lawsuits if they relate to an "ingredient" also used in war-related vaccine development. Another provision blocks Senate-approved legislation to bar government contracts with corporations that have moved their headquarters offshore to avoid U.S. taxes.</P>
<P>Senate Democrats say they will try to strip out these provisions before passing the legislation. But they won't fight the anti-union measures--or any of the Big Brother-style provisions of the bill, such as new restrictions on the Freedom of Information Act.</P>
<P>Indeed, new House Minority Leader Nancy Pelosi (D-Calif.)--the "San Francisco liberal" who is supposed to revitalize the Democratic opposition--showed her willingness to work with the White House by voting for the homeland security legislation.</P>
<P>Meanwhile, Republicans stumbled when it came to pushing through a bankruptcy "reform" law that will make it much harder for ordinary people to get out from under a big debt burden. Credit card companies and banks have been pushing for this for years. But in an eleventh-hour vote last Friday, anti-abortion House Republicans stripped out a provision that would have made it more difficult for pro-life demonstrators facing fines resulting from violent attacks on abortion clinics to declare bankruptcy. As a consequence, it's unlikely that the legislation will make it through Congress before next year.</P>
<P>This scuffle reveals that even with a majority in both houses of Congress, fights among Republicans could slow down the GOP juggernaut. And even some conservatives see a backlash in the making as Republicans push their pet projects. "Many Republicans in Congress want the president to be hard-line on their issues," said Norman Ornstein of the right-wing American Enterprise Institute. "But that carries risks for the president's re-election."</P>

