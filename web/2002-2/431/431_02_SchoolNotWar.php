<P><font face="Arial, Helvetica, sans-serif" size="2"><b>SAY NO TO BUSH'S WAR ON IRAQ</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"Money for our schools, not war"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 22, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"MONEY FOR schools, not for war!" is the demand of a protest called for November 20 by the Chicagoland Student Antiwar Network (CSAWN). The recently formed network is made up of students from a dozen area universities, city colleges and high schools.</P>
<P>Through organizing for November 20, activists are finding a growing layer of students interested in opposing George W. Bush's war on Iraq. "Organizers have been blown away by the number of schools that have responded to e-mails about the protest--places where we didn't even know there were antiwar groups are saying that they're mobilizing to come," said Kirstin Roberts, a Harold Washington College student and one of the organizers of the protest.</P>
<P>And the protest will specifically link the billions being wasted on Bush's war for oil to the billions being stolen from education and other social services. Students have been organizing on their campuses with tabling and banner-making events, and they've gotten wide support from citywide antiwar organizations.</P>
<P>Other schools are planning antiwar actions for November 20 as well. In New York City, the Columbia University Antiwar Coalition is organizing for a day of action on campus that day. Last week, Columbia graduate students organized a protest that drew between 100 and 150 people. Plans are underway for a public debate with the College Republicans.</P>
<P>At New York University (NYU), activists are building for a walkout against the war November 20. Several professors have pledged to honor the walkout. Afterward, NYU students will hook up with a citywide antiwar demonstration of high school and college students. </P>
<P>At the University of Texas in Austin, a debate between the Young Conservatives of Texas and the Campus Coalition for Peace and Justice turned out some 300 people. The winner of the debate was clear--as audience members cheered on the antiwar side and hissed at the right-wingers all night.</P>
<I><P>Candice Amich, Warren Craig and Laura Durkay contributed to this report.</P>

