<P><font face="Times New Roman, Times, serif" size="4">Stop Washington's war</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>This is what they want in Iraq</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 22, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WEAPONS INSPECTORS were rolling into Iraq as <I>Socialist Worker</I> went to press. But that hasn't stopped the Bush administration from moving ahead with its war plans. Even before inspections began, the White House had declared that Iraq was in "material breach" of the new United Nations (UN) Security Council resolution passed November 8.</P>
<P>What was Iraq's "breach"? Firing on a U.S. warplane patrolling in the so-called "no-fly zone" over Iraq--even though these zones were imposed unilaterally by the U.S. and Britain and never had anything to do with the UN.</P>
<P>The resolution that the UN Security Council passed unanimously earlier this month has the same heads-I-win-tails-you-lose character. The Iraqi government is required to state whether it has weapons of mass destruction. If it says that it does have them, that's a "material breach"--and therefore a justification for war. But if it says that it doesn't, that's a "breach," too--because Washington will claim this is a "false statement or omission."</P>
<P>The Bush gang may wait a few weeks to manufacture an "incident" with inspectors, but that doesn't mean war isn't on the agenda. U.S. troops and equipment are already in the Gulf, and more are on the way. "We're making preparations every day," bragged Deputy Defense Secretary Paul Wolfowitz. "I don't want to start saying exactly when we're at peak readiness, but it would be a terrible mistake for anyone to underestimate our ability to act if needed."</P>
<P>The nightmare that ordinary Iraqis will face--after enduring more than 10 years of war and sanctions already--is horrifying to imagine. But the Bush gang doesn't care--and they don't mind who knows it. At a recent speech, Bush's top adviser Karl Rove was asked if the administration was concerned about the possibility that 200,000 Iraqis might die in a U.S.-led invasion. "I'm more concerned about the 3,000 who died on 9/11," Rove said.</P>
<P>You couldn't find a clearer statement of the administration's cold-hearted attitude to the suffering of Iraqis--or its cynical exploitation of the September 11 attacks as a justification for a war to protect oil profits and promote U.S. power.</P>
<P>UN weapons inspections won't stop Bush's gang from launching a new Gulf War. We need to build a movement that stands up to Washington's war makers--even as they make hollow claims about pursuing peace. No more blood for oil!</P>

