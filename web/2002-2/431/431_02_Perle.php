<P><font face="Times New Roman, Times, serif" size="4">Administration hawk lashes out at Europe</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>And he talks of morality?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 22, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ADVOCATE A war that is certain to kill thousands of innocent Iraqi civilians? No problem. But fail to back that war to the hilt? You must lack a "moral compass." This is the world according to Pentagon hack Richard Perle.</P>
<P>Last week, the administration's leading military adviser ranted to Britain's <I>Guardian</I> newspaper about European leaders who failed to give their all-out support to Washington's war drive against Iraq. "I think Europe has lost its moral compass," Perle said. "Many Europeans have become so obsessed by the prospect of violence they have failed to notice who we are dealing with."</P>
<P>The super-hawk accused Germany's Chancellor Gerhard Schr&ouml;der of aiding Saddam Hussein. "Germany has subsided into a moral numbing pacifism," Perle fumed. And he went on to imply that chief United Nations (UN) weapons inspector Hans Blix isn't up to the job of disarming Iraq. </P>
<P>During the interview, Perle gave his hit list of targets after Iraq--and included not only the so-called "axis of evil" countries Iran and North Korea, but also Syria. "There are some people you can't do deals with," Perle said. "You could not do a deal with Hitler, and you can't do a deal with Saddam Hussein or with North Korea." </P>
<P>Maybe Perle should add his own name to that list. Early on in the buildup for "regime change" in Iraq, he put forward the idea that the U.S. might need to use nuclear weapons to get the job done. "No strategist would reject, in principle, using nuclear weapons against Iraq," Perle said earlier this year. Now that's a "moral compass."</P>

