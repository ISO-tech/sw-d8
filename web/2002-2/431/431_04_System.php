<P><font face="Times New Roman, Times, serif" size="4"><b>Not bad for a dead guy</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MARK TWAIN received more than 500 votes in La Crosse County as a write-in candidate for Wisconsin's Third District congressional seat in the November 5 election.</P>
<P>The La Crosse Coalition for Peace and Justice initiated a write-in campaign for Twain after Rep. Ron Kind voted for the House resolution giving George W. Bush authority to use force against Iraq--the only Democratic lawmaker from Wisconsin to vote yes.</P>
<P>Twain received write-in votes for governor, attorney general and every La Crosse County office--including district attorney and sheriff. "I was surprised at how many voted for Mark Twain," Guy Wolf of the LaCross Coalition said. "Our goal was 500 for the whole district because it's difficult to write-in a name with all the different kinds of ballots in the district&#133;We did this with spending no money and starting the campaign only two weeks before the election."</P>
<P>Wolf said the coalition's campaign received national publicity and raised awareness about a potential war in Iraq. "We need to continue to ask Ron Kind to hold the administration accountable for a war in Iraq," Wolf said. "Ron Kind's position on the Iraq war resolution--and not Ron Kind--attracted the Mark Twain vote," he said.</P>
<P>--<I>La Crosse [Wis.] Tribune,</I> November 13, 2002</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Oscar for best protester</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SHOUTING "GO home USA," 40 actors drafted by the army to portray protesters staged a mock confrontation with 350 soldiers descending on a fictitious Arab town.</P>
<P>The Army's 14th Cavalry Squadron, based in Fort Lewis, Wash., was training for urban warfare at the Southern California Logistics Airport. The mission? To keep the peace and purge a terrorist cell.</P>
<P>Civilian actors were paid to play citizens whose mood about the Americans ranged from "eager to please" to "downright hostile." "This is the conflict of the future," said Lt. Col. Jim Cashwell.</P>
<P>--Associated Press, November 11, 2002</P>

