<p><a name="oppose"></a><font face="Times New Roman, Times, serif" size="4"><b>We have to oppose corporate unionism</b></font></P>
<font face="Times New Roman, Times, serif" size="3">
<B><P>Dear <I>Socialist Worker</I>, </P>
</B><P>A struggle against corporate unionism is brewing within American Federation of State, County and Municipal Employees (AFSCME) Local 3299--a local that covers virtually the entire state of California.</P>
<P>The executive board was elected after a vote that was very poorly publicized--allowing a handpicked slate of go-along puppets to win. Additionally, $11 million is being sucked out of our local and into the coffers of the union International, with no accounting to the membership. And outrageously, the new constitution that is being offered to us for a vote has no allowance for setting up a shop steward system.</P>
<P>Those of us who object to this will be picketing and hopefully holding a press conference at the AFSCME International Convention in Las Vegas during the week of June 23, 2002. Our struggle right now is to educate and mobilize our own rank and file.</P>
<B><P>Janice Rothstein,</B> San Francisco</P>

