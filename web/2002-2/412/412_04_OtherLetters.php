<p><a name="King"></a><font face="Times New Roman, Times, serif" size="4"><b>King's "militant nonviolence"</b></font></P>
<font face="Times New Roman, Times, serif" size="3">
<B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>In reading Paul D'Amato's article "Why pacifism is a dead-end strategy" (<I>SW,</I> May 31), I was reminded of some common misconceptions about Martin Luther King's stance regarding nonviolence versus pacifism.</P>
<P>In 1963, King stated, "I am no doctrinaire pacifist." Indeed, he effectively agreed with Floyd McKissick's statement: "Self-defense and nonviolence are not incompatible." </P>
<P>Instead, King "always tried to be what I call militantly nonviolent." In this way, he conceded that "pure nonviolence&#133;cannot easily attract large masses."</P>
<P>However, "the principle of self-defense, even involving weapons and bloodshed, has never been condemned, even by Gandhi," who King revered. He advocated pure nonviolence (or pacifism) only in demonstrations, saying, "The question [is] not whether one should use his gun when his home [or person] was attacked, but whether it [is] tactically wise to use a gun while participating in an organized demonstration."</P>
<P>Thus, I submit a slight modification to your motto: "Militant nonviolence if we may, armed self-defense if we must."</P>
<B><P>David Bliven, </B>Oceanside, N.Y.</P>

