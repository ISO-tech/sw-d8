<p><a name="Myth"></a><font face="Times New Roman, Times, serif" size="4"><b>Myth of a two-state solution</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>Thanks for the great article by Eric Ruder about why the "two state" solution is unworkable in Palestine (<I>SW,</I> June 7).</P>
<P>The "two state" solution is a myth. Neither the Israelis nor their friends in Washington have the slightest intention of actually applying it. Thus, just a few days ago, they both rejected even the watered-down notion of a "temporary state"--a nonsense idea that Hosni Mubarak of Egypt and other U.S. vassals in the Middle East came up with as a theoretical state-on-paper solution which they hoped would mollify the masses.</P>
<P>The original Zionist plan, born in Switzerland in the heyday of 19th-century European colonialism, claimed a "land with no people for a people with no land." So in the Zionist mindset, Palestinians do not exist and never existed. The most the Palestinians will get will be similar to the "homelands" for Black Africans set up by the former apartheid regime in South Africa.</P>
<P>Small wonder that the Israeli state and the apartheid government were such close allies, even developing and testing nuclear weapons together, with the tacit approval of the U.S. government.</P>
<P>What hypocrisy!</P>
<B><P>Tareq I. Ali,</B> London</P>

