<P><font face="Times New Roman, Times, serif" size="4"><b>Don't have illusions in Cuba</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>Avery Wear's letter "We shouldn't echo the right on Cuba" (<I>SW,</I> June 7) misses what should be key to socialists' attitude toward Cuba.</P>
<P>Former President Jimmy Carter's calls for democracy may be liberal cover, but they aren't a cover for the exact same agenda as Bush's. As <I>Socialist Worker</I> argued, Carter represents a part of Corporate America that wants to do business in Cuba--while Bush wants to continue the Cold War policy of isolating the regime.</P>
<P>As revolutionary socialists in the U.S., we oppose all attempts by "our" government and "our" bosses to tell Cuban workers (or anyone else for that matter) what sort of government they should have.</P>
<P>But it isn't because Cuba "won national liberation" or because its government is progressive. The Cuban Revolution--like other national liberation movements of the past 50 years--didn't end class society.</P>
<P>For example, socialists rightly supported the victory of ZANU-PF against the white colonial regime of Rhodesia (in the African country now called Zimbabwe). But after winning national liberation, ZANU-PF President Robert Mugabe eventually opened up the country to the International Monetary Fund. Unfortunately, national liberation is not a permanent state of affairs as long as the U.S. bullies the world with bombs and loans.</P>
<P>We oppose the embargo and right-wing attacks on Cuba because we side with Cuban workers, who have no independent unions and are exploited by their government. Our main enemy is at home, but any illusions in state-run capitalism in Cuba are a trap.</P>
<B><P>Adam Turl,</B> Chicago</P>

