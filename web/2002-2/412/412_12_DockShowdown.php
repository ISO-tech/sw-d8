<font face="Times New Roman, Times, serif" size="5"><b>Showdown on the docks</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Ken Morgan and Brian Belknap</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 28, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE SHOWDOWN shaping up on the West Coast waterfront between the bosses' Pacific Maritime Association (PMA) and more than 10,000 members of the International Longshore and Warehouse Union (ILWU) could be the biggest U.S. labor battle in years. </P>
<P>The stakes are huge. With the ILWU's contract expiring July 1 and the PMA playing hardball in negotiations, a strike or lockout looks possible. That would have a national impact. West Coast ports support nearly 4 million U.S. jobs, generating $723 billion for the overall economy in 2000. </P>
<P>"It's like a speeding freight train that's about to hit the United States," said Robin Lanier, who represents companies like Wal-Mart, Nike and Target as executive director of the West Coast Waterfront Coalition. But as long as waterfront workers take the hit, that's fine with the greedy corporations that Lanier speaks for.</P>
<P>"A strike, lockout or slowdown would be very damaging," Lanier told reporters. "But failing to address the problems at the terminals would be equally if not more costly over the long term."</P>
<P>The PMA claims that it needs to modernize ports in order to remain competitive. What the bosses are really after is to destroy the power of the union through outsourcing the jobs of clerks and speeding up the pace of work.</P>
<P>They're demanding the end of the union hiring hall--something that the ILWU won more than half a century ago after a general strike. Plus the PMA wants to eliminate half of all clerk jobs by moving to computerized link-ups, which would allow the work to be contracted out to nonunion operations in open-shop states such as Utah, Idaho or Texas. </P>
<P>"We're not against technology--it just has to be negotiated," ILWU Communications Director Steve Stallone told <I>Socialist Worker</I>. "Number one, however the work of loading and unloading of ships is done and however the documenting of the cargo is done, it will always be done by ILWU members, and ILWU members will be trained on the new equipment to do it. Number two, we get a portion of the increased wealth that this higher productivity brings. Number three, the use of that technology doesn't cause speedups or make the work unsafe. We've always negotiated everything around those principles, and we have every intention of doing so." </P>
<P>In 1999, when contract negotiations broke down, waterfront workers agreed to work past the expiration of their contract, but used "work-to-rule" tactics to tie the ports in knots. The PMA was brought to its knees in a matter of days.</P>
<P>The ILWU has a fighting tradition that dates back to the 1934 strike up and down the West Coast, which won union jurisdiction in all ports and a union hiring hall. The union has proudly taken solidarity action, not only in support of other dockworkers on the East Coast and internationally, but over such important political issues as supporting the struggle against apartheid in South Africa and the former Pinochet dictatorship in Chile.</P>
<P>In April 1999, for example, ILWU members shut down West Coast ports for a day on behalf of death row political prisoner Mumia Abu-Jamal.</P>
<P>Now, the port bosses are aiming to weaken this militant union--and in so doing push back all unions. But the ILWU won't stand alone. Already, members of the East Coast dockworkers union--the International Longshoreman's Association (ILA)--in Charleston, S.C., are repaying the support that they got from the ILWU when state officials tried to railroad five workers on felony charges after a police assault on their picket line.</P>
<P>ILA Local 1422 President Ken Riley will be on hand in the Bay Area for a June 27 solidarity rally, along with AFL-CIO Secretary-Treasurer Rich Trumka, International Brotherhood of Teamsters President James Hoffa and labor leaders from Europe and Colombia.</P>
<P>"We have support from Japan, New Zealand, Italy, Spain and Australia," Stallone said. "The International Transport Workers Federation and the International Dockworkers Council have both pledged to support us. People know that every time there's been a problem, the ILWU has come to their aid."</P>
<P>Now it's time for us to come to their aid. Support the ILWU! Their fight is our fight!</P>

