<P><font face="Times New Roman, Times, serif" size="5"><b>Detained student activist wins bond</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Keeanga-Yamahtta Taylor</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 28, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AHMED BENSOUDA, the University of Illinois activist detained in May by the Immigration and Naturalization Service (INS), was released on bond last week. An immigration judge granted Bensouda's request for bond during a hearing at the Federal Building in Chicago. Meanwhile, some 100 activists rallied outside in a show of support.</P>
<P>Claiming that he represents a "security risk," federal prosecutors argued that Bensouda should remain behind bars while he awaits a decision on whether he will be allowed to remain in the U.S. But the real reason that Bensouda was picked up is because he's an activist in pro-Palestinian political groups organizing to pressure the University of Illinois to divest financial holdings in companies that do business with the Israeli apartheid state.</P>
<P>Federal officials closed the first hearing after Bensouda's arrest, citing "national security" as the reason. INS officials tried to have Friday's hearing closed as well, claiming that they wanted to present "secret evidence." </P>
<P>But it became clear that the authorities have zero evidence on Bensouda when FBI agents called two of his close friends on the day of the hearing in another fishing expedition for information to bring to court.</P>
<P>Now that he is out of detention, Bensouda says that he plans to fight around his own case--and to support the struggles of other detainees for their rights.</P>

