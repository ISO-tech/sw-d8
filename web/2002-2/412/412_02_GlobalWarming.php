<P><font face="Times New Roman, Times, serif" size="4">New evidence of the dangers of global warming</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Why won't Bush take action?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 28, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>GLOBAL WARMING is coming soon, bringing disease and bacteria with it. According to a report published in <I>Science</I> magazine last week, the rise in temperatures caused by the "greenhouse effect" is likely to have devastating health consequences for dozens of animal species--including humans.</P>
<P>Researchers say that bugs, parasites, bacteria, viruses and other disease-carrying organisms that are usually restricted by cold temperatures could now invade new territory as the climate warms. "Climate change is disrupting natural ecosystems in a way that is making life better for infectious diseases," Andrew Dobson, a Princeton University researcher and co-author of the study, told the Associated Press. In Hawaii, for example, climate changes resulted in an epidemic of malaria among birds--wiping out nearly all birds living below an elevation of 4,500 feet.</P>
<P>Human beings aren't exempt either. As warming continues, scientists expect more frequent and severe outbreaks of diseases such as malaria, yellow fever, West Nile virus and dengue fever.</P>
<P>Authors of the study point to Rift Valley fever, a horrible disease that can cause victims to go blind and vomit blood. Rift Valley fever spread across the Red Sea for the first time in 2000, killing 200 people in Yemen and Saudi Arabia. Researchers say that the fever's range is expanding due to the warming climate.</P>
<P>"We are seeing lots of anecdotes, and they're beginning to tell a story," Dobson told the <I>Boston Globe</I>. "It's a much more scary threat than bioterrorism."</P>
<P>But don't tell that to the Bush administration. Although the Environmental Protection Agency finally admitted in June that global warming is reality, it claims that people should "adapt." Among the recommendations, the "health impacts [of warming] can be ameliorated through such measures as the increased availability of air conditioning."</P>
<P>No need to put new regulations on the White House's corporate pals to stop spewing the pollution that is trapping heat in the atmosphere and causing global warming?</P>
<P>The administration's maneuvering would be laughable if the stakes weren't so high. Any sane society would have taken steps to deal with global warming long ago.</P>
<P>We have to stand up to the Bush gang and their Big Oil buddies as they continue to put our lives on the line for their right to pollute.</P>

