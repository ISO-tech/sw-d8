<P><font face="Times New Roman, Times, serif" size="5"><b>Full of praise for the bigots</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 28, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH addressed the religious right bigots of the Southern Baptist Convention (SBC) in June--and had the gall to praise them for their "tolerance."</P>
<P>In a televised speech via satellite, Bush declared that "Baptists&#133;were among the earliest champions of religious tolerance and freedom"--and praised the group for upholding the separation of church and state.</P>
<P>Dubya must not have meant Rev. Jerry Vines. Speaking the night before Bush, Vines, the former president of the SBC, told thousands of delegates that "religious pluralism" was to blame for the problems of the U.S.</P>
<P>"Christianity was founded by the virgin-born son of God, Jesus Christ," he ranted. "Islam was founded by Muhammad, a demon-possessed pedophile who had 12 wives, the last one of which was a 9-year-old girl."</P>
<P>Vines went on to add: "And I will tell you Allah is not Jehovah either. Jehovah's not going to turn you into a terrorist that'll try to bomb people and take the lives of thousands and thousands of people."</P>
<P>Not that SBC leaders had a problems with Vines' thoughts. Newly elected SBC President Rev. Jack Graham said Vines' comments were "an accurate statement." And outgoing SBC President Dr. James Merritt declared that, "Historically, he is on solid ground."</P>

