<P><font face="Times New Roman, Times, serif" size="4">Washington rakes in the corporate cash</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>For sale to the highest bidder</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 28, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"FOR SALE to the highest bidder." Those words should hang over the doors to the U.S. Capitol building.</P>
<P>Last week, Republicans raked in a whopping $30 million in soon-to-be-outlawed "soft money" campaign contributions at a gala Washington fundraiser. More than 6,000 political insiders and industry donors gathered at the luxurious Mayflower Hotel to watch George W. Bush and other Republicans simper their way into some easy cash.</P>
<P>Drug bosses led the way. GlaxoSmithKline donated at least $250,000 for the privilege of being the chief corporate sponsor of the event. It was joined by Pfizer, Eli Lilly, Bayer AG and Merck--all of which donated more than $50,000 each.</P>
<P>Of course, the generosity of the drug industry might have something to do with the fact that, just two days earlier, Republicans unveiled their plan for providing prescription drug benefits to senior citizens. Under the GOP plan, seniors would be required to buy insurance for drugs from private companies or through a managed-care plan. </P>
<P>It's not like the Republicans need the money--or the Democrats either, for that matter. According to a new report by C-Span's <I>Roll Call,</I> at least 40 members of the Senate--23 Republicans and 17 Democrats--were worth more than $1 million last year. </P>
<P>But disclosure laws are so loose that experts say the number is probably much higher.</P>

