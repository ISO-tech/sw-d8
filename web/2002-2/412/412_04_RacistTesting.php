<P><font face="Times New Roman, Times, serif" size="5"><b>How racist testing adds injury to insult</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 28, 2002 | Page 4</font></p></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>, </P>
</B><P>"If Martin Luther King Jr. had decided to comply with the laws, we would still have Jim Crow laws on the books." That was what Los Angeles school board member Genethia Hayes recently told Superintendent Roy Romer in a discussion about testing. </P>
<P>Federal and state laws require schools to administer high-stakes tests like the Stanford 9 and a High School Exit exam. But they are racist and class-biased. The LA school board voted 4 to 1 to investigate alternatives to the Stanford 9, such as portfolio assessments, as a way to measure student achievement.</P>
<P>For decades, African American and Latino students have tested lower than white and Asian students. But the tests are better measures of class background than academic achievement. For example, second-grade students who are just learning English are tested on words like "bistro," "regatta" and "skiing."</P>
<P>Beyond the cultural biases, it's an outrage that this is the one arena where there is equality in the education system--the same test is given to all students in California. Yet schools in California's poor and minority communities have drastically fewer resources than other schools in the state. Overcrowded, crumbling facilities, insufficient books and materials, teacher shortages and fewer college preparation courses are the rule, not the exception in these schools. </P>
<P>These tests are used to rank schools on an Academic Performance Index (API), which is the basis for some state funding. The schools with good scores get money, and the schools that do poorly get funds cut. In other words, injury is added to insult as these racist tests help reinforce unequal funding for city schools!</P>
<P>Outside the school board meeting, 250 students, teachers and parents marched through downtown Los Angeles chanting, "End racist tests--city schools deserve the best," and "Education is a right, not just for the rich and white!"</P>
<P>This vote is a small victory, but a victory for everyone fighting against racist testing. </P>
<B><P>Karl Swinehart,</B> Los Angeles</P>

