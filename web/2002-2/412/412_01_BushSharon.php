<P><font face="Times New Roman, Times, serif" size="5"><b>Bush backs Israel's apartheid</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 28, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH announced his long-awaited "peace plan" for the Middle East on June 24. But Bush's "peace" proposal is an endorsement of war--Israel's half-century-old war against Palestinians.</P>
<P>And from Palestinians, it demands nothing less than unconditional surrender. Even the zealously pro-Israel mainstream media had a hard time pretending that Bush's plan was balanced.</P>
<P>The very first condition is that Palestinians get rid of Yasser Arafat. Imagine the outcry if Bush had declared that Israeli Prime Minister Ariel Sharon was "unacceptable" because he was "encouraging, not opposing terrorism"--which is what Sharon does on an almost daily basis with his orders for invasions of Palestinian-controlled areas in the West Bank.</P>
<P>But Bush didn't bat an eyelash in declaring the U.S. government's right to decide who gets to run the Palestinian Authority (PA). "When the Palestinian people have new leaders, new institutions and new security arrangements with their neighbors," Bush said, "the United States of America will support the creation of a Palestinian state, whose borders and certain aspects of its sovereignty will be provisional until resolved as part of a final settlement in the Middle East."</P>
<P>In other words, after Palestinians dump Arafat, after they elect new leaders and accept new political structures, after this new leadership convinces Arab countries to go along with the U.S., and after it somehow gets Israel to sign on as well--only then will the U.S. be willing to begin discussing the boundaries of a Palestinian statelet, which would have no real powers independent of Israel anyway.</P>
<P>Meanwhile, Sharon plainly took Bush's speech as a green light for more attacks, declaring the same day that Israel was "preparing to launch a massive operation in the Gaza Strip against the Hamas operation."</P>
<P>The hypocrisy is stunning. After all, since when has the U.S. government cared about democracy in the PA? Palestinian critics of Arafat have long opposed the corruption and repression of the PA. They were ignored, while the U.S.--via that well-known guardian of democracy, the CIA--helped the PA build up its repressive apparatus.</P>
<P>The U.S. and Israel would never tolerate a freely elected PA government that reflected Palestinians' basic democratic demands--such as an immediate end to Israeli occupation or the right of return for Palestinian refugees. Arafat was acceptable because he was willing to bargain away these demands.</P>
<P>Bush's plan is little more than a U.S. seal of approval for Israel's continued occupation. For Palestinians, that means a never-ending nightmare of curfews, closures and desperate poverty--punctuated by the Israeli military's savage offensives.</P>
<P>Last weekend, Israeli troops opened fire on a marketplace in Jenin--the very scene of horrific slaughter in April--killing four people, including three children. Why? The market violated a curfew. Yet in his speech, Bush didn't spend a moment on the Israeli reign of terror that Palestinians endure, day after day after day.</P>
<P>This seemingly slavish devotion to Sharon has led some to argue that Sharon is running U.S. policy in the Middle East--that the Israeli tail is wagging the U.S. dog. But this analysis misses the larger point about Israel's role as the U.S. government's watchdog to protect its interests in the Middle East.</P>
<P>Under Bush, the watchdog has been given a long leash. But if the U.S. wanted to, it could yank its watchdog's chain with a variety of measures--from withholding economic and military aid to voting against Israel in the United Nations.</P>
<P>If Washington sides with Israel, it's because the U.S. supports Sharon and his goals, however much damage Israel's tactics may do to U.S. alliances with Arab governments.</P>
<P>It's not as if any other U.S. president has been interested in justice for Palestinians. Every "peace plan," whether offered by Bill Clinton or Ronald Reagan, has aimed to win Palestinians to surrendering--and abandoning their fight for a genuine state.</P>
<P>The politicians may call this surrender "peace." But no one else should. We stand with Palestinians in their struggle against Israel's apartheid--and insist that there can never be "peace" without justice.</P>

