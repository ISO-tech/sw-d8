<P><font face="Times New Roman, Times, serif" size="5"><b>Why won't Hoffa fight UPS?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By a member of Teamsters Local 804</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 28, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--More than 1,000 workers turned out to demand a good contract from UPS at a rally hosted by Teamsters Local 804 outside New York City. President James Hoffa Jr. promised to reject UPS's proposals for givebacks.</P>
<P>Lead negotiator Ken Hall revealed that the company is proposing a measly yearly raise of 30 cents an hour--the union is asking for $1.25 an hour. But that was the only specific information we got about negotiations on our next contract--the current Teamsters contract at UPS expires July 30.</P>
<P>Otherwise, there was nothing more than macho talk about shoving the company's offer "up their ass"--without saying anything about what our own demands are.</P>
<P>Hoffa took time during this speech to attack U.S. corporations for going to Mexico and taking away American jobs. But this essentially pits U.S. workers against Mexican workers and plays into the bosses' hands, while Hoffa himself hasn't offered a strategy to fight for better wages and conditions at UPS.</P>
<P>As the rally ended and the sound system played "Viva Las Vegas," many members who hoped to hear more about the status of the negotiations came away disappointed. As for the one demand that Hall mentioned, a yearly $1.25 wage increase is too low for a starting demand. If we split the difference between the union offer and the company offer, it's a mediocre 80 cents an hour. </P>
<P>Unfortunately, it looks like the Teamsters leadership is trying to keep our expectations low. They shouldn't be. According to a recent union mailing, UPS's profits have doubled since 1996. That means we should get twice as good a contract as we did in 1997, when we shut them down for three weeks.</P>
<P>A few members passed out flyers calling for UPS Teamsters to reject any contract without the following:</P>
<P>--Strong language against layoffs</P>
<P>--1,000 new full-time jobs in Local 804</P>
<P>--All new full-time jobs paying top rate</P>
<P>--$12 an hour starting pay for part-timers</P>
<P>Teamsters around the country should campaign for a "no" vote on any offer from UPS without these types of demands. And we need to let management know that we're prepared to strike in order to win them.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>UPS targets militants</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>FOR ITS part, UPS is preparing for a fight--by stepping up its attack on union activists. For example, last week UPS fired Dawn Stanger, a shop steward in Teamsters Local 597 in Vermont.</P>
<P>After singling her out for discipline over the long haul--for "crimes" such as going to the water fountain for a drink after she asked for permission--UPS terminated her on the trumped up charge that she was "stealing time."</P>
<P>And this is just the most recent example of UPS battering its workers in Vermont with petty disciplinary actions. Ever since the successful Teamsters strike against UPS in 1997, management has put pressure on union activists across the country.</P>
<P>It's no surprise that UPS is going even harder at workers at this critical time. We need to challenge this harassment--and push for our agenda.</P>
