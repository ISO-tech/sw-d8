<P><font face="Times New Roman, Times, serif" size="4">Unionists take on global "rat race to the bottom"</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Organizing in the maquiladoras</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 28, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>, </P>
</B><P>I recently returned from a solidarity delegation that visited Nuevo Laredo, in the Mexican state of Tamaulipas. I met and talked with several workers and labor organizers about workplace conditions and also the battle for human rights.</P>
<P>In the "rat race to the bottom" of global capitalism, corporations compete to pay the lowest wages, weaken unions, lower labor standards and rake in the most profits. Accelerated by NAFTA, the U.S.-Mexico border has become one of the most crucial and inspiring theaters of the battle between the workers and global capitalism. </P>
<P>The workers are employed in maquiladoras, which are export-processing plants owned by corporations that send goods there to be assembled and then ship them back to the U.S. or other countries.</P>
<P>The corporate bosses are aided by the corrupt "official" union, the Central de Trabajadores Mexicanos (CTM), which has failed to represent workers and has been involved in repression when workers attempt to assert their rights. </P>
<P>But despite this harsh situation, there is a growing number of workers who are standing up. The Comite Fronterizo de Obreras (CFO) is a grassroots organization that has dedicated itself to educating workers about their rights and how they can organize.</P>
<P>Workers involved with the CFO face abusive supervisors, long hours, lack of safety precautions, dangerous chemicals, countless injuries and on-the-job discrimination. For women, it's even worse. Many companies do not meet the legal requirements for pay, and since they know the women are working out of necessity (often with children), they are pushed around and paid less than men. Yet many of these same abused and exploited women are in the forefront of the struggle--at tremendous risks to themselves. </P>
<P>Despite firings and blacklistings, CFO local organizers--known as "promotoras"--are enthusiastic. They have a slogan in the CFO: "Defend, Organize, Teach and Win." The CFO has been remarkably successful and courageous, and it deserves our unconditional support in its fight for justice! </P>
<B><P>Adam Meyer</B>, Austin, Texas</P>

