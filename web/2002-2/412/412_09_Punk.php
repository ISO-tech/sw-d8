<P><font face="Times New Roman, Times, serif" size="4">25 years since punk began</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Revolution rock?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 28, 2002 | Page 09</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>I NEARLY fell off my chair a few weeks ago when I read that John Lydon--a.k.a. Johnny Rotten of the legendary punk rock band the Sex Pistols--had offered to sing for the Queen of England's "Golden Jubilee," celebrating her 50-year reign.</P>
<P>Twenty-five years before, Lydon was singing the Sex Pistols' song "God Save the Queen." Despite being banned from the airwaves for ruthlessly ridiculing the idiocy of the monarchy, the song hit number one on the charts anyway: "God save the Queen/She ain't no human being/There is no future/in England's dreaming." The song captured what life was like for a generation of young people growing up with little to look forward to and the hypocrisy of the Queen's celebration.</P>
<P>Punk rock, with its high-volume dissonance, driving rhythms and confrontational style, started in the U.S. in the mid-1970s, with bands like the Ramones and New York Dolls leading the way. It crossed the ocean and took off in Britain in 1977 as the perfect antidote for the bland lies of daily life--and the equally bland mainstream rock and roll that dominated the airwaves.</P>
<P>"I can't describe the feeling I get from listening to this record," wrote the editor of the punk fanzine <I>Sniffin' Glue</I> in a review of a single by another punk legend The Clash. "This single brings rock back to where it should be. Back in the hands of the kids&#133;I hope that every kid who buys this single listens to it. Realise that we have got to act now. 1977 is the Queen's jubilee year, well let's make it our year as well."</P>
<P>Songs like Sham 69's "If the Kids are United," Stiff Little Fingers' "Alternative Ulster," The Mekons' "Fight the Cuts" and the Dead Kennedys' "Kill the Poor" gave punk a reputation for putting forward explicitly left-wing ideas.</P>
<P>Of course, not all punk rock was political, and unfortunately, the despair reflected in the music in some cases opened up the door to the expression of vile and racist ideas. To combat the growing tide of racism and fascist organizing in Britain, socialists and antiracists formed Rock Against Racism (RAR) in late 1976.</P>
<P>RAR made an explicit connection between the mainly white punk rock audience and Black reggae and ska music--something that bands like The Clash had done musically. RAR concerts featured punk bands like the Adverts, The Clash, the Angelic Upstarts and Sham 69, along with reggae acts like Steel Pulse and Peter Tosh.</P>
<P>In April 1978, RAR held its Carnival Against Racism, where some 100,000 packed a London park for a concert that served to build the movement against Britain's Nazi National Front.</P>
<P>Like any movement in popular music, there were people who wanted to make a buck off punk rock. Punk's "do-it-yourself" ethic came into contrast quickly with Sex Pistol manager Malcolm McLaren's mass marketing of the punk rock "uniform"--ripped T-shirts, pants with safety pins and studded collars. Meanwhile, bands like The Clash grappled with the question of how to be independent while signing record deals with a corporate giant like Sony.</P>
<P>Even so, punk shook up rock music in the 1970s and 1980s and helped to pave the way for movements like grunge, goth, Two-Tone ska and more. And the musicians that followed the initial punk explosion--people like Billy Bragg, The Pogues, Rancid, NOFX, and The (International) Noise Conspiracy--show that punk rock and left-wing politics can go hand in hand, although sometimes with less-than-stellar musical results.</P>
<P>Today, The Clash and Sex Pistols are marketed as "classics." Punk clothes are available in every mall in America. And retro-punk bands like The Hives and The White Stripes are getting massive airplay on mainstream radio.</P>
<P>None of that is bad. But it's good to remember that the legacy of punk rock isn't just about style and marketing--it's also about the politics of the time that influenced the music. As fanzine writer Lucy Toothpaste wrote in 1977, "Punk isn't gonna change the world. (But punks might, one of these days.)"</P>

