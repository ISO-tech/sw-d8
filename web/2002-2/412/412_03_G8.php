<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>G-8 fat cats offer crumbs to the poor</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 28, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>A REMOTE location high in the mountains, surrounded by rough terrain and wild animals, with surface-to-air missiles at the ready. Sounds like something from a James Bond movie--the secret lair of criminal underworld heads plotting for world domination.</P>
<P>Not exactly. It's the Canadian resort town of Kananaskis, Alberta--and currently the site of the annual summit of the Group of Eight (G-8) nations.</P>
<P>The G-8 consists of the U.S., Russia, Japan, Germany, France, Britain, Italy and Canada. For people who claim to be meeting to discuss stopping world poverty, they couldn't have picked a site more removed from the misery of the poor.</P>
<P>Not to worry--George W. Bush and the others plan to discuss Africa over lunch. Bush says that he's concerned with what Treasury Secretary Paul O'Neill found on his mission to Africa with rock star Bono.</P>
<P>"I went to those troubled lands," O'Neill told him, "and I believe this: With the right combination of aid and accountability--from both rich nations and poor ones--we can accelerate the spread of education, clean water and private enterprise throughout Africa."</P>
<P>But Bush's commitment to increasing spending on education in Africa amounts to a meager $200 million over the next five years. "America will not build this new Africa," Bush declared. "Africans will. But we will stand with the African countries that are putting in place the policies for success."</P>
<P>Of course, "policies for success" will undoubtedly mean African nations opening up their markets to U.S. multinationals. And it will mean "standing with America" in its "war against terrorism"--or else. Indeed, Bush's top priority for the G-8 meeting is peddling his war drive, with the aim expanding the slaughter to Iraq.</P>
<P>Given the real goals of the meetings, Bush and Co. knew that they had to find the most secluded place possible. They wanted to make certain that they avoided a replay of last year's summit in Genoa, Italy, where hundreds of thousands protested the meetings.</P>
<P>In spite of the militarized atmosphere, some 3,000 protesters demonstrated in the Alberta capital of Calgary on June 24. Meanwhile, in Europe, global justice activists organized dozens of protests against the a summit of the 15 leaders of the European Union, culminating in a 150,000-strong march led by the Seville Social Forum.</P>
<P>A few days earlier, unions organized a one-day general strike against government cuts in unemployment insurance--the first general strike in Spain in close to a decade.</P>
<P>The global justice movement in the U.S. was pushed back after September 11 and Bush's war drive. But around the globe--including in the U.S.--the bitterness that fueled this movement hasn't gone away.</P>
<P>We can show the fat cats that another world is possible.</P>

