<P><font face="Times New Roman, Times, serif" size="4">Carousel workers win eight-month strike in Chicago</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"S&iacute; se pudo, s&iacute; se pudo"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Diana Bernal and Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 28, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>HIGHWOOD, Ill.--"S&iacute; se pudo! S&iacute; se pudo!" The chant, "Yes we did!" filled the basement of the Highwood Lanes bowling alley June 22, where more than 100 friends, family and supporters gathered to celebrate the victory of strikers at Carousel Linens, Inc., in suburban Chicago.</P>
<P>The 38 workers, primarily Latina women immigrants, ended their eight-and-a-half month strike for union recognition with a union contract and strong gains.</P>
<P>When <I>Socialist Worker</I> went to press, not all of the contract's details were available. But the contract includes a 32 percent wage increase over its three-year life, free family health care services at a union-run clinic, an extra sick day and paid holiday and strong immigrant rights protections, according to the Union of Needletrades, Industrial and Textile Employees (UNITE).</P>
<P>Forcing the company to recognize the union means more than simply increased wages and benefits, striker Celita Vasquez told <I>Socialist Worker</I>. "Well, most importantly, [the victory means] respect. Respect as workers and human beings," she said. "Now they have to treat us with respect."</P>
<P>That lack of respect, sweatshop working conditions--and pitiful $5.15 an hour wages for workers with 10 years of experience--pushed the Carousel workers to organize and strike in October 2001. They turned to UNITE on the recommendation of workers at another nearby laundry, Skokie Valley Laundry, which had joined UNITE after a four-year battle for union recognition.</P>
<P>At the victory celebration, the Carousel workers paid special tribute to their sisters and brothers from Skokie Valley, who missed their lunch hour to walk the picket line with Carousel workers every day. "We were [afraid to strike at first], but the Skokie Valley workers helped and supported us throughout the entire strike," said Carousel worker Teresa Vasquez.</P>
<P>In addition to the strike, the union worked with community, religious, student and political organizations to pressure Carousel customers to cancel contracts with Carousel until Carousel boss Scott Close settled. The pressure forced major corporate customers Abbott Laboratories and ARAMARK to drop Carousel.</P>
<P>"We were very grateful for the support we received from other unions and political organizations," said UNITE organizer Augustin Smilovitz. "And grateful to the workers, who over eight months were constantly in the struggle, not thinking about the weather or their families, but thinking only of the struggle."</P>
<P>Even in the anti-immigrant post-September 11 political climate, the Carousel workers continued to press their case. </P>
<P>"[Workers should] not be afraid&#133;to fight for their rights, their rights as workers and human beings," said Celita Vasquez. "[The strike] has changed my thinking. Now I am not afraid."</P>

