<P><font face="Times New Roman, Times, serif" size="5"><b>Saying no to women's rights</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 28, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>AFTER INITIALLY indicating its support, the Bush administration is delaying congressional ratification of the Convention on Elimination of All Forms of Discrimination Against Women.</P>
<P>The international treaty has already been signed by 169 countries. Its main provisions ban forced marriage and require equal access to education, health care and property rights for women.</P>
<P>Not too controversial. But Janice Crouse says that the treaty is the product of a "frivolous and morally corrupt agenda"--and would "legalize prostitution and open the door for the homosexual agenda." It even, she says, attacks Mother's Day.</P>
<P>The only reference in the pact to prostitution is a requirement that countries suppress it. Gay rights aren't mentioned. Nor is Mother's Day. But that hasn't stopped Crouse, a senior fellow at the right-wing Beverly LaHaye Institute and delegate for the Bush administration to the United Nations Children's Summit.</P>
<P>Crouse's word seems to be good enough for the Bush gang. In May, the White House asked the Senate Foreign Relations Committee to postpone hearings on the treaty, pending a review by John Ashcroft's Justice Department.</P>
<P>Neither Ashcroft nor his Justice Department flunkies are explaining why they need to review the pact. But it's no secret that, even in an administration filled with bigots, Ashcroft's fanaticism is second to none.</P>

