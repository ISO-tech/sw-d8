<P><font face="Times New Roman, Times, serif" size="4">Legal victory vindicates two activists</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Justice long overdue</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 21, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>TWO ENVIRONMENTAL activists won their lawsuit against Oakland, Calif., police and FBI agents who framed them as terrorists.</P>
<P>Unfortunately, Judi Bari won't be able to celebrate. She died of cancer in 1997 in the midst of the fight to clear her name. But an Oakland jury's decision last week that three Oakland cops and three FBI agents violated the civil rights of Bari and Earth First! organizer Darryl Cherney is long overdue vindication.</P>
<P>On May 24, 1990, Bari and Cherney were driving through Oakland on their way to a concert organized as part of the struggle to save redwood forests in northern California when their car exploded. The homemade pipe bomb--studded with nails--was under Bari's seat. </P>
<P>Yet during their investigation, Oakland police and the FBI managed to blame the two activists. Bari and Cherney were accused of transporting the explosives that went off.</P>
<P>The evidence? Investigators found nails and bits of towel in the wreckage of the car--items that were found in a search of Bari's home. "That practically every home contains nails and towels was somehow ignored by investigators, who also managed to ignore any evidence that pointed to another perpetrator," the <I>San Jose Mercury News</I> wrote in an editorial.</P>
<P>The charges against them were quickly dropped, but Bari and Cherney kept up a legal fight to clear their names. They came up with the evidence to expose the shadowy background of the FBI and its relations with local cops.</P>
<P>For example, Frank Doyle--the FBI agent in charge of the investigation--turned out to have been a supervisor with Squad 13, a joint FBI and Oakland police antiterrorism squad that collected files on Bay Area radical groups.</P>
<P>And these are the very goons that John Ashcroft wants to "reorganize"--and give a green light to harass any activist they want.</P>

