<P><font face="Arial, Helvetica, sans-serif" size="2"><b>AFGHANISTAN</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Karzai installed in power by a council of rival warlords</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Victory for a U.S. stooge</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 21, 2002 | Page 5</font></p></P>
<font face="Times New Roman, Times, serif" size="3"><P>"DEMOCRACY" HAS come to Afghanistan. At least according to the U.S. government and its stooge, Hamid Karzai, the newly elected president of Afghanistan. But if you don't accept their word for it, there's room for doubts.</P>
<P>Beginning June 11, more than 1,600 delegates attended a loya jirga, a tribal council supposedly designed to allow the free election of a new president for Afghanistan. Although the loya jirga was supposed to have begun the previous day, a 24-hour delay was called to allow for last-minute arm-twisting.</P>
<P>Gathering in a high-tech air-conditioned tent once used for beer festivals and donated by Germany, delegates thought they'd be choosing between Karzai, the U.S.-backed interim president; the senile former King Mohammed Zahir Shah; and Burhanuddin Rabbani, the warlord thug who heads the Northern Alliance and ran the country from 1992 to 1996.</P>
<P>Some choice.</P>
<P>But before delegates got to vote, U.S. envoy Zalmay Khalizad told them that the former king would not be a candidate. Then Rabbani suddenly withdrew his name from consideration without explanation--paving the way for Karzai's election by a wide margin. </P>
<P>"The gang leaders of Afghanistan have agreed to let Mr. Karzai remain leader of the next interim government," wrote journalist Robert Fisk in Britain's <I>Independent</I> newspaper. "But at present, those same Mafia bosses are running many of the major cities of Afghanistan."</P>
<P>As Sima Simar, the Women's Affairs Minister, told the <I>Independent</I>: "This is not a democracy. This is a rubber stamp. Everything has already been decided by the powerful ones."</P>
<P>But it's hard to understand how anyone could have expected different. As a Human Rights Watch report noted, "Delegates have been handpicked by warlords determined to defend regional fiefdoms&#133;An institution that promised the start of a democratic future could instead legitimize a return to the abusive past."</P>
<P>As <I>Socialist Worker</I> went to press, the assembly had broken up without having accomplished its most important tasks--agreeing on rules for selecting a new cabinet or composing a new parliament.</P>
<P>Even members of the national commission that organized the assembly had to admit that the result was chaos. "This puts a question mark over the loya jirga's legitimacy," one commission member, Syed Massood, told the <I>Washington Post</I>. "We made promises to the people of Afghanistan, and now I don't know how we can look them in the face."</P>

