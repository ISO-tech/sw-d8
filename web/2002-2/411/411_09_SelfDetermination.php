<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Marxists and the right to self-determination</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 21, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WE MARXISTS are internationalists. "Workers of the world, unite!" was the call of Marx and Engels in <I>The Communist Manifesto</I>. "You have nothing to lose but your chains."</P>
<P>Does this mean, then, that Marxists turn their backs on national oppression? "Since Marxism is based upon international workers' solidarity," a one-sided Marxist might argue, "we therefore denounce all forms of nationalism as a hindrance to that international workers' unity."</P>
<P>Critics of Marxism who argue that it cares only about economics and has nothing so say about questions of oppression might attribute such views to a Marxist. But it was Marx who said that a nation that oppresses another cannot be free, and Lenin who argued that nations oppressed by imperialism have the "right to self-determination."</P>
<P>Nationalism is a very real phenomenon that cannot simply be ignored. In the U.S., national patriotism--the idea that "USA is Number One," is used to bind workers to rulers, to convince American workers that they have the same interests as wealthy profiteers and their friends in the White House.</P>
<P>In short, workers here are encouraged to believe that they have more in common with George Bush--a scion of the rich who is backed by oil and gas interests--than they do with Iraqi workers that Bush proposes to bomb.</P>
<P>This is true in every country, as all are divided by class, and each state, though it represents the interests of the ruling, capitalist class, seeks the support of its population for its own narrow interests through an appeal to nationalist sentiment.</P>
<P>Nevertheless, socialists make a distinction between the nationalism of the oppressors and the nationalism of the oppressed. We do this not because we are nationalists--on the contrary, we oppose the elevation of any nation over that of another, or the elevation of one culture over another.</P>
<P>Precisely because we are internationalists we argue for full equality between nations, and that such equality can only be established by granting oppressed nations the right to self-determination. Only in this way can workers in the oppressed countries unite with workers in the oppressor country.</P>
<P>These ideas were first developed by Marx around the question of England's oppression of Ireland. "In all the big industrial centers in England," wrote Marx, "there is a profound antagonism between the Irish proletariat and the English proletariat. The average English worker hates the Irish worker as a competitor who lowers wages&#133;He regards him somewhat like the poor whites of the Southern states of North America regard their black slaves. This antagonism among the proletarians of England is artificially nourished and supported by the bourgeoisie. It knows that this antagonism is the true secret of maintaining its power."</P>
<P>Marx concluded therefore that socialists must support Ireland's separation from England. "It is a precondition of the emancipation of the English working class," he argued, "to transform the present forced union (i.e. the enslavement of Ireland) into equal and free confederation if possible, into complete separation if need be."</P>
<P>He applied the same methodology to slavery in the U.S. South, arguing that working-class emancipation could not be accomplished without destroying slavery. "Labor in the white skin can never free itself as long as labor in the black skin is branded," he argued in his monumental work <I>Capital.</P>
</I><P>Lenin compared the right of self-determination to the right of divorce (writing at a time when it was difficult for a woman to obtain one). We support the ability of women to dissolve a marriage not because we want all marriages to break up, he argued, but because equality of the sexes cannot exist where women are legally bound to their husbands.</P>
<P>Likewise with national oppression. Workers' unity cannot be built without workers in an imperialist or oppressor country supporting the right of self-determination of the oppressed nation.</P>

