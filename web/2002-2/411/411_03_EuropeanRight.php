<font face="Times New Roman, Times, serif" size="5"><b>Behind the right's success in Europe</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 21, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>CONSERVATIVE PARTIES won a big majority of seats in the French legislative elections June 16. Their victory--along with the election success of Nazi Jean-Marie Le Pen in the first round of France's presidential election in April--has raised the question of whether Europe is veering to the right.</P>
<P>Last week's result in France was the latest in a series of victories for the European right, which in recent years has taken control of governments in Italy, Spain, Portugal, Austria, the Netherlands and Denmark.</P>
<P>But the right's advance isn't a one-way street. On the contrary, it reflects a process of political polarization between right and left following the disappointment with "center-left" governments that ruled Europe's main countries since the late 1990s.</P>
<P>During his unsuccessful campaign against incumbent President Jacques Chirac in April, former Socialist Party Prime Minister Lionel Jospin declared that his program was "not socialist"--and imitated Chirac's law-and-order politics. This opened the door for Le Pen, who played on workers' frustrations by scapegoating immigrants.</P>
<P>But while Le Pen outpolled Jospin in the first round of the presidential elections, revolutionary left candidates together gained an unprecedented 10 percent of the vote.</P>
<P>Jospin's defeat and the crisis in the "governmental left"--the Socialist, Communist and Green parties--set the stage for the right to win the legislative elections. But Chirac and the right don't have a mandate for their antiworker program.</P>
<P>Abstention rates hit a record 39 percent--and the unions that mobilized for anti-Le Pen demonstrations are preparing for a fight.</P>
<P>"As if to fire a warning shot, several labor groups representing public transport workers, pediatricians, emergency-room physicians and air traffic controllers have scheduled strikes and protests for the coming week to demand improved work conditions," the <I>New York Times</I> reported.</P>
<P>Many on the French left anticipate a rerun of 1995, when a newly elected conservative government attacked state workers' pensions, setting off a public-sector general strike. That government was tossed out of office two years later.</P>
<P>A similar dynamic can be seen in Italy, where Prime Minister Silvio Berlusconi was driven from office after only seven months in 1994, when his attacks on the retirement system led to the biggest labor protest in Italian history.</P>
<P>After defeating a center-left government in elections last year, Berlusconi is in office again--but has been met with resistance. For example, when the government tried to push a law to make it easier to fire workers, unions mobilized a mass demonstration in March and a general strike a few weeks later.</P>
<P>Likewise, the 500,000-strong demonstration in Barcelona in March--under the slogan "Against a Europe of capital and war"--showed the potential to fight the growing attacks by conservative governments and employers.</P>
<P>Spanish unions held a 200,000-strong protest in Seville June 9 and called a general strike for June 20 during the European Union summit.</P>
<P>This fall, the European Social Forum will meet in Italy, providing an opportunity to discuss how to build an alternative to Europe's "governmental left"--a left based on workers' struggles, opposition to the corporate agenda and solidarity and genuine socialism.</P>

