<P><font face="Times New Roman, Times, serif" size="5"><b>Hitler and Sharon have quite a bit in common</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 21, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>Dave Courtenay-Quirk's letter objecting to the slogan "Sharon and Hitler are the same," (<I>SW,</I> June 7) misses the point by a wide mark.</P>
<P>Chants at demonstrations are not analytical essays. The point is not to equate the ideologies down to the last sociological detail, but to point out that both Hitler's regime and the Israeli regime are based on racist ethnic cleansing. </P>
<P>The slogan is meant to put the lie to the idea that Zionism was--or is--somehow an effective or legitimate response to anti-Semitism.</P>
<P>Dave says it's okay to draw parallels between Nazi Germany's treatment of the Jews and the Zionist state's treatment of the Palestinians, since "even Israeli soldiers are making the same comparison." Then what is the problem with making the same comparison in a chant?</P>
<P>Not only is there a well-documented history of Israel's ethnic cleansing toward Arabs, but also a history of Nazi-Zionist collaboration, which can be found in books like Lenni Brenner's<I> Zionism in the Age of the Dictators</I>.</P>
<P>Activists most horrified by the comparison are those who still haven't completely broken with Israel and Zionism. We shouldn't let their squeamishness determine our position.</P>
<B><P>Paul D'Amato,</B> Chicago</P>

