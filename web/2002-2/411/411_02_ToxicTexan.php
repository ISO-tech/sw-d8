<P><font face="Times New Roman, Times, serif" size="5"><b>EPA guts regulation for power plants</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 21, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE "TOXIC Texan" is at it again. After the Environmental Protection Agency (EPA) released a report admitting that global warming was a reality, the Bush gang had to reassure the energy bosses that they wouldn't have to worry about tougher pollution laws.</P>
<P>So last week, the EPA announced that it was relaxing pollution regulations for coal-burning power plants.</P>
<P>Under existing laws, utilities have to install additional pollution controls when they upgrade, expand or modernize plants to produce more electricity. The energy companies have long complained that this prevents them from making more profits.</P>
<P>So their buddies in the White House decided to give them a helping hand. Under the revised Clean Air Act, the pollution "threshold" will be lowered, so that utilities can expand production without worrying about improvements.</P>
<P>So the energy bosses will be able to get away with murder--literally. That's because even EPA officials admit that the pollution from coal-burning power plants is responsible for thousands of deaths and chronic illnesses a year.</P>
<P>As John Walke, director of the Natural Resources Defense Council's Clean Air Program, told reporters, "Today's long-dreaded rollback of federal clean air protections means that, under the Bush administration, the EPA has become the Environmental Pollution Agency."</P>

