<P><font face="Times New Roman, Times, serif" size="5"><b>Crane operators win in the port of Charleston</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 21, 2002 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>CRANE OPERATORS in the port of Charleston, S.C., won a stunning victory last month when a walkout by 32 of 35 of the workers shut down the docks for almost an entire weekend.</P>
<P>The workers, who operate the giant cranes that move cargo containers, are members of International Longshoremen's Association (ILA) Local 1422. Yet because they are employed by the Ports Authority, a state agency, they are barred from collective bargaining under South Carolina's "right-to-work" laws.</P>
<P>But management tried to take away daily overtime pay after a normal daily shift and instead pay overtime only after 40 hours in a week.</P>
<P>The workers were furious. On May 17, a supervisor fired one worker and ordered security to escort him off the docks. But the rest of the workers walked out and immediately went to the Local 1422 hall to meet, local president Kenneth Riley told <I>Socialist Worker</I>.</P>
<P>Management immediately threatened the rest of the workers with dismissal and immediate replacement. "We told them that if they did that, that our members weren't going to work with trainees--it's too dangerous," Riley said.</P>
<P>After losing millions of dollars, management caved and rehired the workers--with their old pay schedule and working conditions. This was de facto recognition of the union--and follows the successful campaign for freedom for the Charleston Five, all but one whom were members of Local 1422.</P>
<P>It also is a big step toward overcoming historical racial divisions on the docks. While the crane operators are all white, Local 1422 is virtually all Black. As Riley put it, "This is a big victory."</P>

