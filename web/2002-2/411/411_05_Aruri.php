<P><font face="Times New Roman, Times, serif" size="4">Naseer Aruri on U.S. support for Israel's war:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"Bush's embrace of Sharon"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 21, 2002 | Page 5</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>NASEER ARURI has written and edited numerous books about the Palestinian struggle, the latest being <I>Palestinian Refugees: The Right of Return</I>. He talked to <I>Socialist Worker</I> about the U.S. government's role in backing Israel's war against Palestinians.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<P>THERE WAS always a question about the role of the United States--as the catalyst for peace, the conciliator. This assumption has been challenged more widely than before.</P>
<P>I think the new theme is that after Sharon's sixth visit to the White House, there seems to be an end to the pretense that the U.S. is an "honest broker."</P>
<P>Here is Bush saying about Sharon, "Every time he comes here, I learn something. He's a 'good teacher.'" That comes on the heels of another statement during the previous visit--number five--when Bush called Sharon "a man of peace."</P>
<P>Concretely, what did Bush gain from the teaching? First of all, when Sharon came on his last visit on June 10, he invited himself because he knew that his "student" had a long meeting with the Saudi crown prince.</P>
<P>Sharon came in order to veto the idea of talks, and Bush obliged when he made a statement--with Sharon at his side--that he didn't think conditions were right for talks.</P>
<P>He was asked about the regional conference that Secretary of State Colin Powell floated a couple months ago as the administration was facing a lot of criticism after the massacres in Jenin, Nablus and Ramallah.</P>
<P>Bush acknowledged that there's urgency, but that it's not time yet. What this really means is that even the nuances have been abandoned--that is to say that the American embrace of Sharon's position seems to be complete.</P>
<P>Both Sharon and Bush have run out of conditions to place on Arafat. First, there was cease-fire. Then, he had to destroy the network of terror. And lastly, reform and reorganization of the government.</P>
<P>So we have moved from Bush not wanting to be involved at all in the Middle East conflict when he came to power in January 2001 to being involved wholly on the Israeli side.</P>
<P>I find this unprecedented, having studied U.S. presidential policies towards Palestine since Nixon. American policy for the past 35 years has been extremely deceptive--claiming to be the peacemaker while acting as a co-belligerent.</P>
<P>And yet every American president and secretary of state found it necessary to couch this support in diplomatic language that was intended to be deceptive. Now that's been abandoned.</P>
<P>So Sharon went back having accomplished what he wanted.</P>

