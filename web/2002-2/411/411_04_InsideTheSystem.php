<font face="Times New Roman, Times, serif" size="5"><b>Grizzly bears for global justice</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 21, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>IN THE search for ever-more remote locations to hold summits of the world's rich and powerful, government lackeys thought they found a good one: the Canadian Rockies.</P>
<P>But when George W. Bush and other world leaders meet for the Group of Eight summit next month, it won't be angry global justice protesters that they need to worry about. It will be the wildlife.</P>
<P>The local grizzly bear population will be coming out of hibernation around the same time that the summit takes place--and the bears will be hungry, skinny and stressed, according to wildlife biologists. So authorities have started "arresting" the bears in advance of the two-day summit.</P>
<P>Using helicopters, authorities plan to airlift bear "snipers" armed with tranquilizer darts through the 2,500-mile Kananaskis wilderness. Security agents have also received information on what to do in the case of an attack from cougars, wolves and, worst of all, pregnant moose.</P>
<P>"They have just started to calve and can get really protective over their young," says wildlife biologist Jon Jorgenson. "An angry moose could chase you right up a tree."</P>
<P>--<I>The Observer,</I> May 19, 2002</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>When a bribe isn't a bribe</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IT'S LEGAL for executives from a U.S company to bribe foreign officials to reduce their company's taxes or customs payments. That's what a judge ruled in April, when he heard the case of American Rice, the largest rice miller in the U.S.</P>
<P>Earlier this year, company executives were charged with violations of a U.S. anti-bribery law. According to the Justice Department, they bribed Haitian officials in order to get their customs payments and taxes reduced.</P>
<P>But lawyers argued that even if a bribe was paid, it isn't illegal because the law only covers bribes made to "obtain or retain business." The judge agreed.</P>
<P>--Focus on the Corporation, April 25, 2002</P>

