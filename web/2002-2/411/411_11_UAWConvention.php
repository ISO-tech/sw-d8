<P><font face="Times New Roman, Times, serif" size="4">Las Vegas convention was an empty spectacle</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Where is the UAW headed?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar, National Writers Union/UAW Local 1981</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 21, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LAS VEGAS--With looming plant shutdowns, a declining membership and tough contract negotiations ahead, the United Auto Workers (UAW) faces its greatest challenge in years. Yet at the union's constitutional and bargaining conventions here June 3-10, UAW leaders offered only Vegas glitz and scripted proceedings to block dissent.</P>
<P>The leadership's allies read every convention resolution aloud, followed by prearranged speeches from the floor.</P>
<P>One of the few opposition speakers was Gregg Shotwell, a Delphi worker and delegate from Local 2151 in Coopersville, Mich., and author of the popular <I>Live Bait &amp; Ammo</I> shop newsletter. "I know who you are, brother, I read your e-mail," Yokich said, and cut off Shotwell's mic.</P>
<P>There were plenty of progressive resolutions and militant-sounding speeches. But the real politics were left to a parade of Democratic politicians live and on video.</P>
<P>The only purpose of the convention was to crown the new leaders chosen by the Administration Caucus, which has reigned over the UAW longer than Queen Elizabeth has held the throne in Britain.</P>
<P>The most outrageous example came when UAW Vice President Richard Shoemaker rammed through a transfer of $75 million out of the strike fund into an "emergency" account run by the IEB. The Administration Caucus also pushed through an amendment abolishing Region 2 in Northern Ohio to punish regional director Warren Davis for breaking his promise to retire.</P>
<P>Some in the small group of delegates from the UAW Solidarity Coalition and the UAW New Directions Movement did get the floor. This writer, a delegate from UAW Region 4, made a motion to overturn the IEB's cutoff of strike benefits and the removal of the UAW charter from Local 2036, locked out by Accuride in Henderson, Ky., since 1998. Many delegates applauded the motion, and only small minority cheered Yokich's vicious--and lying--response.</P>
<P>The motion was ignored. Nevertheless, opposition delegates circulated some 2,000 flyers explaining the truth about Local 2036--and a number of delegates privately thanked them.</P>
<P>About 25 people turned out to hear former Local 2036 president Billy Robinson tell the real story of the struggle at Accuride and how UAW officials abandoned it. And it was the incoming UAW President Ron Gettelfinger who authorized the strike at Accuride that led to the lockout.</P>
<P>The UAW did win a short strike last week at Johnson Controls parts plants in four states. But the new leadership has no strategy except collaboration and retreat.</P>
<P>It will be up to rank-and-file UAW members to take the initiative in the battles ahead.</P>

