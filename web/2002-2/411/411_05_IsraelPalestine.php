<font face="Times New Roman, Times, serif" size="5"><b>Israel sets stage for new assault</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 21, 2002 | Page 5</font></p></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE ISRAELI government seemed on the edge of a full-scale military assault as <I>Socialist Worker</I> went to press, following a Palestinian suicide attack on June 18. The Islamist group Hamas claimed responsibility for the bombing, which killed 20 people and injured dozens.</P>
<P>Palestinian Authority (PA) chief Yasser Arafat condemned the attack. But Israeli Prime Minister Ariel Sharon dismissed Arafat's words and continued to blame the PA leader. This could set the stage for a number of military options--from another crushing Israeli assault on the West Bank or Gaza to the capture and forced exile of Arafat.</P>
<P>Israel's right-wing parties--which hold a key position in Sharon's government--have made no secret of their hope that the Israeli military will eventually carry out an offensive to drive Palestinians out of the West Bank altogether.</P>
<P>As usual, the U.S. media broadcast all the horrifying details of the suicide bombing--while virtually ignoring reports of the steady killings of Palestinians, including more than 90 in the last six weeks. Likewise ignored in the U.S. press is Israel's use of torture against Palestinian prisoners--something highlighted by the abuse of Marwan Barghouti, a high-level PA official.</P>
<P>Barghouti has been subjected to sleep deprivation and "position torture"--in which his hands and legs are shackled to a small chair angled to slant forward, so that he can't sit in a stable position--since his arrest eight weeks ago.</P>
<P>Meanwhile, under the cover of Sharon's recent visit to the U.S., Israel began construction of a fence to seal off the Occupied Territories. The fence is Israel's latest method for carving up Palestinian land--and blocking the possibility of any viable Palestinian state emerging from future negotiations.</P>
<P>In addition to the vast network of checkpoints and settlements that reach deep into the territories, the fence will lead to the confiscation of even more Palestinian land.</P>
<P>Already, Israeli settlements control 40 percent of the territory of the West Bank--while the PA controls only 18 percent, according to a recent study by the Israeli human rights group B'Tselem.</P>
<P>Israeli right-wingers claim that the fence will keep suicide bombers from entering Israel--but even government officials admit that it's next to impossible to patrol that much distance.</P>
<P>And the fence will only increase the suffering and desperation in the West Bank--the very ingredients that cause young Palestinians to believe that their only hope of striking back against oppression is to blow themselves up.</P>
<P>Israel's ongoing reign of terror against Palestinians is the real source of the conflict. It's time to expose the truth--and to demand that the U.S. end all support for Israel's barbarism.</P>

