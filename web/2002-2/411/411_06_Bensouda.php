<P><font face="Times New Roman, Times, serif" size="5"><b>Oppose this modern-day witch-hunt</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Keeanga-Yamahtta Taylor</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 21, 2002 | Pages 6 and 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ARAB STUDENT and activist Ahmed Bensouda is vowing to fight a political attack on him by the Immigration and Naturalization Service (INS). The student at the University of Illinois in downstate Urbana-Champaign (UIUC) was picked up in late May--and remains in custody because of a visa violation.</P>
<P>Bensouda had been in the country on a student visa, but left school a year and a half ago in violation of the visa. But the real reason that Bensouda was picked up was because he's an activist in pro-Palestinian political groups organizing to pressure UIUC to divest financial holdings in companies that do business with the Israeli apartheid state.</P>
<P>In fact, after Bensouda was first detained, federal officials went to his apartment the next day to download material from his computer. Moreover, Bensouda has told friends that he was subjected to racial slurs and repeated questioning about his political ideas.</P>
<P>"The question remains: was my only big mistake being an Arab/Muslim on an outdated visa?" Bensouda said in a recent statement. "Not according to the way the authorities have been operating. My case has been designated a 'special case,' i.e. one related to national security, as of Wednesday June 12--at my first and only court hearing thus far, which was closed to the public and media and was unrecorded."</P>
<P>Now, prosecutors are saying that they will present secret evidence against Bensouda at his next hearing, which is scheduled for June 21.</P>
<P>But Bensouda is calling on his supporters to fight this attack. "In spite of being denied basic rights and the intimidation tactics of the federal authorities, I plan to fight my case," he said in his statement.</P>
<P>"In fact, I will fight to remain in the country permanently&#133;The most effective way to support us in our time of need is to hold those institutions accountable for their actions. The relevant authorities need to feel the heat of public awareness and the discontent with the way they wield their power to trample over so many people's lives. We need your help."</P>

