<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Bush White House declares a first-strike military strategy</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Washington's war on the world</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 21, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"CIA GETS LICENSE TO KILL." If <I>Socialist Worker</I> put this headline on our front page, it would be dismissed as hysterical by pro-war politicians and their media mouthpieces.</P>
<P>But that was the banner headline on the front page of the <I>Chicago Sun-Times</I> June 17--the day after George W. Bush signed an order authorizing U.S. secret agents to murder Iraqi leader Saddam Hussein in "self-defense."</P>
<P>And Saddam is far from the only foreign leader in Washington's sights. Bush will soon unveil a policy of "pre-emptive" attacks against any group or country that gets in Washington's way--on the grounds that they might commit hostile acts.</P>
<P>"President Bush has directed his top national security aides to make a doctrine of pre-emptive action against terrorist groups trying to develop weapons of mass destruction the foundation of a new national security strategy," the <I>New York Times</I> reported. But in reality, this is the latest attempt by the Bush White House to justify military intervention to achieve U.S. interests.</P>
<P>Just look at Central Asia, where Washington used its war on Afghanistan to establish military bases in five oil- and gas-rich countries that were once part of the ex-USSR.</P>
<P>And for all the White House hype about fighting terrorism, the big news last month was the announcement of a $2 billion gas pipeline across Afghanistan--to be built by Unocal, the same U.S. company that eagerly sought a similar deal with the Taliban.</P>
<P>Now, to deflect criticism over the government's mishandling of warnings of the September 11 attacks, Bush is whipping up war fever once again.</P>
<P>But the Republicans aren't the only hawks in Washington. House Minority Leader Richard Gephardt (D-Mo.), who in 1990 voted against the Gulf War against Iraq, called Bush's new murderous policy against Saddam "the appropriate action to take."</P>
<P>Sen. Joe Biden (D-Del.), the head of the Senate Foreign Relations Committee, is also backing Saddam's ouster--and a U.S. occupation of Iraq for "two, four, five years to give the country a chance to be held together."</P>
<P>As Washington fed the unquestioning media its plan for a first-strike doctrine, the Pentagon was launching construction of the dangerous new missile defense system, just days after abandoning an arms control treaty with Russia that barred such weapons.</P>
<P>Meanwhile, Washington's first-strike doctrine will provide justification for other countries around the world to launch similar actions. Israel is bound to claim Bush's policy as a green light for another genocidal attack on Palestinians--funded and supported, as usual, by Washington.</P>
<P>And that's not all. "No place is the risk greater than in South Asia," said former U.S. ambassador to Croatia Peter Galbraith, and a supporter of the new policy. "If India adopted the American doctrine of pre-emption, it risks a nuclear war, with devastating consequences for the world. It's a tricky business."</P>
<P>All this exposes the "war on terror" for what it really is: a new, aggressive phase of U.S. imperialism to consolidate its economic and political power through terrible force.</P>
<P>We can't allow rhetoric about the "war on terror" to put us on the defensive. We need to oppose Washington's military madness now--before it brings new horrors to the world.</P>

