<P><font face="Times New Roman, Times, serif" size="5"><b><i>Stillmatic</I> raises the bar on hip-hop</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Candice Rivas</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 21, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><P><B>MUSIC:</B> Nas, <B><I>Stillmatic,</B></I> Columbia Records, 2001.</P>
</font><P>NO OTHER hip-hop artist takes the reality of poverty and ghetto life and explains its effects on social and political life like Nas.</P>
<font face="Times New Roman, Times, serif" size="3"><P>Nas appeared at the end of the movie <I>John Q,</I> criticizing the health care system, and performed in New York City at a rally against budget cuts.</P>
<P>When <I>Stillmatic</I> came out in December 2001, it was the hottest topic of conversation--mostly because he criticizes Jay-Z's flashy lifestyle, but also because of his opposition to U.S. domestic and foreign policy.</P>
<P>In the hit single, "One Mic," Nas uses the metaphor of a microphone that he would use to spread his voice around the world, and people everywhere would fight back. The video depicts poor people fighting back against the police.</P>
<P>In the last three tracks, Nas' tone gets angrier and sharper, especially about race and class. In "Rule," he not only talks about homelessness and poverty, but also speaks out against the U.S. war. "Everybody wants a shot/In this land of opportunity/Look at what this country's got/There shouldn't be nobody homeless/How can the president fix other problems when he ain't fix home yet?"</P>
<P>In "My Country," Nas opens talking about police brutality, and then about why the prison system is stacked against the poor. He dedicates the song to Che Guevara, Malcom X, Martin Luther King, Patrice Lumumba and everyone fighting for a better world.</P>
<P>"What Goes Around" is a great track in which Nas talks about a variety of issues--drugs, religion, sexism, even nutrition. And Nas talks about the blowback of U.S. policy when he says, "All the wrong doers have it coming back to them a thousand times over&#133;Even the most greatest nation in the world has it coming back to 'em."</P>
<P>He goes on to say, "The Indians saved the Pilgrim/And in return the Pilgrim killed 'em/They call it Thanksgiving/I call your holiday hell day/'Cause I'm from poverty, neglected by the wealthy."</P>
<P>The album isn't perfect. Nas uses derogatory terms toward women and gays a few times. Although Nas does it less than most mainstream hip-hop artists, it's a major weakness that takes away from his political lyrics.</P>
<P>Still, Nas' radical ideas are getting airplay from Harlem to Germany. And <I>Stillmatic</I> definitely raises the bar on what people should expect from hip-hop.</P>

