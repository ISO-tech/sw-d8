<P><font face="Times New Roman, Times, serif" size="5"><b>Facts for opposing the war on workers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Carole Ramsden</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 21, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><P><B>BOOKS:</B> Holly Sklar, Laryssa Mykyta and Susan Wefald. <B><I>Raise the Floor: Wages and Policies That Work for All of Us.</B></I> Ms. Foundation for Women, 2002, 248 pages, $9.95.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>"IF THE U.S. government were a parent, it would be guilty of child abuse." That's the conclusion reached by Holly Sklar, Laryssa Mykyta and Susan Wefald, authors of <I>Raise the Floor</I>.</P>
<P>The facts speak for themselves. "One out of six children is growing up poor&#133;in the richest nation on earth. Globally, the U.S. ranks first in wealth and military might, and 32nd in child mortality under 5 years old." This is only one example of <I>Raise the Floor</I>'s arsenal of statistics.</P>
<P>All the data support one point: More U.S. workers are finding it harder to make ends meet and the government should support policies that boost their incomes. Even the federal antipoverty programs that haven't been slashed fall far short of providing basic necessities, much less easing poverty.</P>
<P>The income the government expects people to survive on--the federal poverty level--and the minimum budget people really need to survive on are two completely different incomes, the book shows. "[A] single person without employment health coverage would need about 190 percent of the federal poverty threshold to meet their minimum needs budgets. Families would need more than double the official poverty level to meet their basic needs," write the authors.</P>
<I><P>Raise the Floor</I> crushes right-wing arguments against welfare, the minimum wage and federal antipoverty spending.</P>
<P>Take the standard argument that raising the minimum wage will "cost jobs." After Congress raised the minimum wage in 1996, unemployment declined to its lowest level in three decades over the next four years, they show.</P>
<P>If anything, bosses are ripping off increasingly productive workers more than ever. "Productivity grew 74.2 percent between 1968 and 2000, but hourly wages for average workers in 2000 were 3 percent lower, adjusting for inflation. Wages for minimum wage workers were 35 percent lower.</P>
<P>"What if wages had kept rising with productivity&#133;? The average hourly wage would have been $24.56 in 2000, rather than $13.74. The minimum wage would have been $13.80 in 2000--not $5.15," the authors write.</P>
<P>The policies <I>Raise the Floor</I> proposes, such as raising the minimum wage to $8 or providing health insurance for all uninsured children, are fairly modest. But the fact that they sound radical in today's Washington just goes to show how far removed our so-called leaders are from the concerns of everyday people.</P>
<P>At less than $10, <I>Raising the Floor</I> is a good buy for anyone who wants to know the facts that refute the politicians' lies.</P>

