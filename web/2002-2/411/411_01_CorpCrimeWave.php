<P><font face="Times New Roman, Times, serif" size="4">Enron--Andersen--Tyco</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Corporate crime wave</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 21, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ARTHUR ANDERSEN, one of the country's oldest accounting firms, was supposed to oversee Enron's accounting methods and certify that the company was sound. Instead, it helped Enron cover up its crooked books--and obstructed justice by shredding incriminating documents, according to the verdict of a Houston jury last week.</P>
<P>Andersen is likely to go belly-up, like Enron before it. But these are only the first episodes of Corporate America's latest crime wave. Company after company used the same shady schemes as Enron during the 1990s "miracle" boom, hiding losses, inflating stock prices and enriching executives. They were the toast of Wall Street as long as the profits rolled in.</P>
<P>But now one after another--Global Crossing, Tyco, Qwest, General Electric--has fallen on hard times. Together, they tell a disgusting story of greed and corruption.</P>
<P>Take the conglomerate Tyco--and its hotshot former CEO Dennis Kozlowski. One day after he resigned last week, Kozlowski was indicted for evading $1 million in New York state taxes on $13 million of art that he bought. But that's a minor misdeed compared to the scams that he oversaw during his decade running Tyco.</P>
<P>Kozlowski used thousands of acquisitions to transform the company from a New Hampshire manufacturer into a Bermuda-based multinational with a quarter of a million employees. In the process, he piled up $27 billion in debt, and now the whole house of cards is collapsing.</P>
<P>Corporate America's defenders claim that Kozlowski is a "bad apple" who taints "responsible" CEOs. But this "bad apple" was celebrated as America's "most aggressive CEO" by <I>Business Week</I> magazine just last year.</P>
<P>Kozlowski may have gone further than some. But not long ago, every corporate boss wanted to imitate him. That's why the list of corporate scandals is growing by the day.</P>
<P>Washington politicians have responded by proposing some watered-down reforms. But even if they survive the corporate lobbyists, "stricter accounting rules" won't mean much. That's because most corporate crimes--like laying off workers or slashing benefits or paying executives astronomical salaries--are perfectly legal.</P>
<P>Throughout the 1990s, downsizing and outsourcing kept workers' wages stagnant while CEO compensation went through the roof. Today, CEOs "earn" an incredible 531 times the average wage for a blue-collar worker.</P>
<P>Millions of workers blame themselves for falling into debt and going bankrupt. They're wrong. The real culprit is a bankrupt system that steals from the poor and rewards the rich.</P>

