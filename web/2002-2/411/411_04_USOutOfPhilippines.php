<P><font face="Times New Roman, Times, serif" size="4">Stepping up involvement in a former U.S. colony</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>U.S. out of the Philippines!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 21, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>U.S. and Filipino officials last week called for deeper U.S. military involvement in the Philippines. After being kicked out by a powerful nationalist movement in 1991, the U.S. military--under the guise of fighting terrorism--began re-deploying personnel to the Philippines in January. </P>
<P>The stated goal was to conduct joint military exercises and train Filipino soldiers in fighting Abu Sayyaf, a brutal kidnap-for-ransom group that was holding a Kansas missionary couple, Martin and Gracia Burnham, and a Filipina nurse, Deborah Yap.</P>
<P>With a July 31 deadline looming, government officials announced an indefinite extension of the U.S. presence. Visiting the capital of Manila in early June, U.S. Deputy Defense Secretary Paul Wolfowitz said: "Our aim is not just to defeat terrorists but to build a foundation of a just, peaceful and tolerant society...[W]e will be open to all kinds of possibilities&#133;[I]t's a misleading impression to suggest to the Filipinos that as soon as the Burnhams are rescued, the Americans will leave the Philippines."</P>
<P>Since January, U.S. troops have doubled from 660 to 1,200. Philippines President Gloria Macapagal Arroyo has also called for U.S. involvement "down to company level."</P>
<P>Because of protests, U.S. troops have been kept away from the front lines. That's the official story anyway. At least one U.S. helicopter has crashed in the area, and there are reports that two U.S. helicopters exchanged fire with Abu Sayyaf in May. </P>
<P>News of deeper U.S. military involvement coincides with the failed rescue Burnhams and Yap. With help from the U.S., Philippines troops ambushed Abu Sayyaf on June 7. Martin Burnham and Yap were killed in the ensuing gun battle, and Gracia Burnham was wounded.</P>
<P>Their lives are fodder for the U.S. grand plan to re-establish military outposts in its former colony.</P>
<B><P>Eduardo Capulong, </B>San Francisco</P>

