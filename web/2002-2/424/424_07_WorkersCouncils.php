<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"It all depends on workers themselves"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 4, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ONE OF the main questions that people have about socialism is how we'll make sure that it's a just society.</P>
<P>They can agree with our basic case--that we need to replace a capitalist system that breeds poverty and suffering with a democratic society organized around meeting people's needs. But how do we know for sure that socialism would be different? Why wouldn't it produce a new class of rulers?</P>
<p><a href="../../Featured/MarxDebate.shtml"><img src="../../Ads/MarxismDebateAdHome.gif" width="144" height="196" hspace="10" align="right" border="0"></a>The starting point for answering these questions is history. Working-class struggles of the past century--especially those that have vied for power, however briefly, against the institutions of capitalism--have given us a glimpse of what a future society run by workers will look like.</P>
<P>These glimpses have something in common, no matter where in the world they happened or when--the involvement of masses of working people, trained all their life to be subordinate, participating in the discussions and decisions about what should happen.</P>
<P>In every revolutionary upheaval in which the working class seriously threatened the rule of capitalism, workers have organized along remarkably similar lines--with a system of workers' councils.</P>
<P>The first appearance of this body was in the 1905 Russian Revolution. The "soviets" (the Russian word for "council") arose as committees in individual workplaces, created to organize for a wave of battles over economic issues. But the need to respond to wider political questions--for example, the use of massive repression by the tsar--led the soviets to make links locally and then regionally.</P>
<P>The soviets were highly democratic and responsive, much more so than anything under capitalism. The ratio of delegates to those they represented was one for every 500 workers, and those workers had the right to immediately recall their delegates.</P>
<P>This same system of workers' councils has arisen again and again in the biggest working-class upheavals--the Spanish Revolution of 1936-37, the Hungarian revolt against Stalinism in 1956, the cordones of Chile in 1973, the workers' commissions in Portugal in 1974-75, the shoras during the Iranian revolution of 1979.</P>
<P>One important point to emphasize is that no socialist or Marxist came up with the idea. The workers' councils developed spontaneously out of the needs of workers involved in struggle. In other words, the answer to the question of how workers can rule comes from the organization of struggles today.</P>
<P>This is very much in keeping with the way that Karl Marx and Frederick Engels developed their ideas about socialism in the middle of the 19th century--in opposition to other socialists who tended to imagine a future utopia with no connection to how it could be achieved.</P>
<P>Marx and Engels began to outline a different way of thinking, focused not on "dogmatically anticipating the new world," as they put it, but rather on "finding the new world" in the old. Rather than imagining a better world disconnected from the question of how to get there, the stress of Marxism is on how the process of "getting there" determines the shape of the better world. </P>
<P>So the guarantee against socialism producing a new elite lies, for Marxists, in the mass participation of working people in creating that new society.</P>
<P>The American socialist Eugene V. Debs summarized the point this way: "In the struggle of the working class to free itself from wage slavery, it cannot be repeated too often that everything depends on the working class itself. The simple question is: Can the workers fit themselves, by education, organization, cooperation and self-imposed discipline, to take control of the productive forces and manage industry in the interest of the people and for the benefit of society? That is all there is to it."</P>
<I><P>This article is adapted from a contribution to the debate between Michael Albert of ZNet and Alan Maass of Socialist Worker. Read the debate at www.socialistworker.org.</P></I>

