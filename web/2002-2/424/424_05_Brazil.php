<P><font face="Arial, Helvetica, sans-serif" size="2"><b>BRAZIL</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Can Lula win on first ballot?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 4, 2002 | Page 5</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE BANKERS and bosses are sending a message to the people of Brazil. Don't vote for Lula for president on October 6--or else.</P>
<P>With international investors pulling their money out, Brazil's stock market fell to a three-and-a-half-year low last week. Likewise, the country's currency, the <I>real,</I> plunged to its lowest level against the dollar since it was introduced a decade ago.</P>
<P>The frantic financial assault is Wall Street's response to the growing possibility that Luiz In&aacute;cio "Lula" da Silva, candidate of the left-wing Workers' Party (PT), could win an outright majority in the first round of the presidential election on October 6.</P>
<P>A new opinion poll shows Lula with 45 percent of the vote--and rumors are spreading that several candidates may drop out and throw their support to him. If Lula doesn't get 50 percent, the top two candidates will go to a run-off election October 27.</P>
<P>Lula's massive support is another sign of the growing resistance to the free market in Latin America. This month's presidential vote comes on the heels of an unofficial referendum in September in which 10 million Brazilians said no to the Free Trade Area of the Americas--Washington's bid to open up every country in the Western Hemisphere, except Cuba, to Corporate America's profiteering.</P>
<P>Lula is winning the support of ordinary Brazilians fed up with poverty and free-market madness. But the former factory worker has spent much of the presidential campaign appealing to business leaders. Last week, for example, he met behind closed doors with dozens of Western bankers and business officials--promising yet again that a PT government would honor Brazil's foreign debt and meet the conditions for a $30 billion bailout from the International Monetary Fund, according to reports.</P>
<P>"Lula would be only slightly different than Cardoso," one Brazilian political analyst told the Associated Press. "Besides, he will have to negotiate and compromise with a Congress that will likely be controlled by the opposition."</P>
<P>Nevertheless, a victory for Lula will raise the expectations of Brazilian workers. Lula is still on record in favor of popular measures such as an increase in the country's meager minimum monthly wage of $55.</P>
<P>If he doesn't deliver, Lula could find himself on a collision course with the poor and working-class Brazilians who put him in office.</P>

