<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Behind Hitchens' lurch to the right</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 4, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>FOR ANYONE who followed Christopher Hitchens' "Minority Report" column in the<I> Nation</I> magazine over the last year, his decision to quit shouldn't have come as a surprise. Since September 11, Hitchens devoted a number of columns to cheering on Bush's "war on terrorism" and denouncing others on the left who didn't.</P>
<P>Not wanting to go quietly, Hitchens spent his last column building the case for war against Iraq--insisting that arms inspections are a waste of time, that Iraq can't be "contained" and that Saddam can be linked to al-Qaeda.</P>
<P>As his parting shot, he even denounced the <I>Nation </I>as "the voice and echo chamber of those who truly believe that John Ashcroft is a greater menace than Osama bin Laden." Now it takes quite a leap into Limbaugh-land to accuse the <I>Nation </I>of being soft on Osama Bin Laden. Last year, it became the main liberal magazine touting the "war on terrorism" as a "just war."</P>
<P>Only time will tell whether Hitchens follows in the footsteps of David Horowitz, the 1960s-radical-turned-McCarthyite-fanatic. But Hitchens has clearly come a long way since the 1960s, when he joined the International Socialists (IS), the ISO's former sister organization in Britain.</P>
<P>Hitchens left the IS and organized socialist politics in the 1970s. Nevertheless, he continued to describe himself as a socialist, even though he advocated some anti-socialist positions, such as opposition to abortion and support for U.S. intervention in the Balkans.</P>
<P>It would be easy to write off Hitchens as a "sellout" who decided that toiling in the vineyards of leftist journalism doesn't pay as well as being a "renegade from the left" on the talk-show circuit. </P>
<P>Hitchens did become something of a talk-show star in 1998 and 1999, when he turned up as the main leftist writer supporting President Clinton's impeachment at the hands of Washington's right-wing establishment. He became a hero to conservatives when he gave evidence to the House impeachment managers against one of Clinton's aides in 1999.</P>
<P>To Hitchens' admirers, his willingness to rat out a friend or dissent from "leftist orthodoxy" represents a "contrarian" commitment to intellectual integrity. This is nonsense.</P>
<P>Most of Hitchens' "contrarian" ideas aren't all that original. His calls for Western bombers to defend "civilization" against the likes of Osama bin Laden and Slobodan Milosevic rehash the same arguments that Cold War liberals used to support Washington against Moscow.</P>
<P>And just what is so intellectually courageous about joining hundreds of other pundits in praising the "war on terrorism"? Bourgeois intellectuals satisfy themselves with the conceit that their individual brilliance allows them to judge society from "above the fray." For the rightward-moving left-wing intellectuals, this is a convenient pose because it portrays increasing acceptance of society's conventional wisdom as an act of intellectual integrity.</P>
<P>Hitchens' acceptance of conventional wisdom has included characterizing protests against capitalist globalization as "protests against modernity" and praising former British Prime Minister Margaret Thatcher for unleashing capitalism's "revolutionary" potential.</P>
<P>For Hitchens to echo these standard "free-market" positions in the recent period of capitalist triumphalism represents less a declaration of independence from the left than a surrender to the intellectual fashions of neo-conservative Washington.</P>
<P>For years, he referenced Marx and Engels' <I>Communist Manifesto</I> in his defense. But quotes from Marx and Engels were increasingly ornaments in Hitchens' writings. They long ago ceased to serve the purpose that Marx and Engels meant for them--as part of an argument about changing the world.</P>
<P>The real surprise about Hitchens' latest rightward lurch isn't that it happened. It's that it didn't happen years ago.</P>

