<font face="Times New Roman, Times, serif" size="5"><b>One million more fall into poverty</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 4, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MEALTIMES ARE depressing for 6-year-old LeeArius Daniels. There's not much work in Pembroke Township, Ill., where he lives--so it's been hard for his mother LaCheir to find employment.</P>
<P>LaCheir and her five kids get $450 a month in food stamps. That's it. Thanks to the Clinton administration's welfare "reform" law in 1996, cash grants were cut off to "able-bodied adults" who can't find work.</P>
<P>LaCheir is willing to work, but with a limited education, no jobs in town, and neither a car nor accessible public transportation to use to commute, there's not much she can do.</P>
<P>When a <I>New York Times</I> reporter researching poverty in the U.S. visited recently, he found that the family's water heater didn't work. There was no food in the cupboards, and LaCheir was skipping meals to make sure that her kids could have what little food was left at the end of the month.</P>
<P>This is the sickening reality for hundreds of thousands of families living in similar circumstances in the U.S. And that number is getting bigger.</P>
<P>According to U.S. Census Bureau statistics, the proportion of Americans in poverty rose significantly last year, for the first time in eight years. Today, 32.9 million Americans are impoverished--11.7 million of them are children. That's an overall increase of 1.3 million poor people since the year 2000. </P>
<P>Part of what makes these statistics so appalling is that, at the same time, the rich are getting richer. According to Census Bureau figures, household income for the richest 20 percent of the population actually increased by an average of $1,000 apiece last year. The top 20 percent got half of all household income, while the poorest 20 percent received just 3.5 percent.</P>
<P>"The Census data show that income inequality either set a record in 2001 or tied for the highest level on record," Robert Greenstein, executive director of the Center on Budget and Policy Priorities, told the <I>New York Times</I>. So while more and more children like LeeArius Daniels are wondering where their next meal is going to come from, corporate crooks and the filthy rich are doing just fine.</P>
<P>Former Tyco CEO Dennis Kozlowski may be in hot water after revelations of how he wasted company money on absurd luxury items--$15,000 for an umbrella stand in the shape of a dog, $445 for a pincushion, and $2,200 for a trash basket.</P>
<P>Of course, those are just the "little" things--he also blew more than $1 million last year to pay for a lavish birthday party for his wife at the Hotel Cala di Volpe on the Italian island of Sardinia. But it's doubtful that he'll even spend a day in jail.</P>
<P>What a sick system where a corporate parasite can spend as much on a trash basket as a poor family like LeeArius' has to survive on for five months. We need a socialist alternative to this world of greed and poverty.</P>

