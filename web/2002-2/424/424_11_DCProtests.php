<P><font face="Times New Roman, Times, serif" size="5"><b>Weekend of protest in Washington</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 4, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WASHINGTON--More than 10,000 people turned out for a weekend of protests against globalization and war.</P>
<P>On September 28, some 7,000 gathered for a rally and march against the global loan sharks at the International Monetary Fund (IMF) and World Bank. The next day, more than 4,000 attended a separate rally and march to oppose George W. Bush's plans to attack Iraq.</P>
<P>Richard Braithwaite, a member of the AIDS group ACT UP, traveled from Philadelphia to attend the Saturday global justice protest. "I know here in America, a rich country, I'm doing well with AIDS," said Braithwaite, who became an activist five months ago when he was diagnosed with HIV. "I was practically dead, I took the medication, and now I'm undetectable. I think of people in poor countries who don't have that option because they're too busy paying back the debt to rich countries like America. Everyone has the right to live."</P>
<P>Braithwaite's determination to speak up against Washington's war on the world's poor could be felt throughout the crowd.</P>
<P>Still, the Saturday protest, organized by the Mobilization for Global Justice (MGJ) and other groups, was considerably smaller than earlier demonstrations. The lower turnout reflects the setback of the global justice movement after last year's September 11 attacks and Bush's "war on terrorism."</P>
<P>One symptom of that disorientation was the MGJ's failure to make the most important issue of the day--the U.S. war drive against Iraq--a part of the protest. In reality, many who came out to protest the IMF were ready to take a stand on the war.</P>
<P>"The U.S. thinks that it can swing its weight around like they can with the IMF and World Bank--and invade anybody they want, set up World Bank Projects anywhere we want," Rachelle Rohrer from Florida State University in Tallahassee, told <I>SW</I>. "Obviously, there's a connection there--exploiting people that can't stand up to the U.S."</P>
<P>Meanwhile, other global justice activists have reacted to the lower level of mobilization by concluding that small acts of defiance are the way forward--instead of working to build the confidence of others to come out and protest.</P>
<P>The so-called "People's Strike," held on Friday, is a case in point. Protesters claimed that they would shut down the city by randomly blocking streets and gluing shut subway stations. The "strike" achieved nothing, other than inconveniencing workers and giving police an excuse to arrest more than 600 protesters.</P>
<P>On Sunday, peace activists organized a completely separate protest to take up the issue of the war drive against Iraq. The turnout of 4,000 people--most of them from the D.C. area--outpaced organizers' expectations.</P>
<P>The demonstration turned out a wide range of people--with a wide range of ideas about what slogans the antiwar movement should take up. When marchers stopped in front of Vice President Dick Cheney's residence, one speaker led protesters in the chant "Peace is patriotic."</P>
<P>Speaker Ben Dalbey from the ISO challenged this idea. "We stand in solidarity with people around the world," Dalbey said. "This is not a patriotic movement. This is not a movement of Americans. This is a movement of the world's citizens that will stand up to this regime."</P>
<P>As Oscar Olivera, the leader of the fight against water privatization in Bolivia, put it at a meeting in the lead-up to the protests, "The victories of the U.S. government are not the victories of the U.S. people. We do not separate the battle against the war against the World Bank and the battle for democracy worldwide."</P>

