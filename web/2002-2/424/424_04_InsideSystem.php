<P><font face="Times New Roman, Times, serif" size="4"><b>Tell the truth, get in trouble</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LORI BLUMKE thought that she was doing her civic duty. When she reported to the Grand Rapids, Mich., District Court for jury duty and was asked if she could be fair to police, she said no, because cops had recently refused to take a report for a coworker who had been assaulted. </P>
<P>She was excused by the prosecutor, but was called for a new case minutes later. "The judge asked me if I still had the same opinion about the police. I said, 'Yes, I do,'" she told reporters.</P>
<P>But instead of simply excusing her from jury duty, Judge David Buter ordered Blumke to perform 24 hours of community service.</P>
<P>Buter says that he offered her a "choice"--between community service and changing her mind about police. "If we are going to be effective spreading the load among the jurors, and we have one who says she can't or won't, I give them the option of community service," said Buter. </P>
<P>Now, Blumke has been assigned to three consecutive Mondays picking up litter, and is also being forced to submit to a Breathalyzer test. "I'm going to miss three days of work. I'm being treated like a criminal because I told the truth," said Blumke. </P>
<P>--<I>Grand Rapids Press,</I> September 20, 2002</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Go to hell, Bill Gates and Microsoft</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>A LOT of people hate Bill Gates. But the popular Internet search engine Google.com really has it in for him.</P>
<P>Currently, if you type "go to hell" into a Google search, the number one result is the Microsoft Corporation. When asked about the search result, a Google spokesperson said that it was just a "coincidence."</P>
<P>Poor Gates. In addition to being compared to Satan, the corporate mogul now has to worry about his fortune, which shrank by more than $42 billion over the past three years.</P>
<P>Of course, he's still the richest man in the U.S by far.</P>
<P>--<I>Computerworld,</I> September 25, 2002</P>

