<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISRAEL</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Setback in Sharon's war on Palestinians</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Israel retreats from Arafat siege</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 4, 2002 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ISRAEL ENDED its 10-day siege of Palestinian leader Yasser Arafat's compound last weekend after coming under a barrage of criticism from around the world.</P>
<P>The pullback was a setback for Israeli Prime Minister Ariel Sharon, who had vowed not to end the siege until Arafat chose to go into exile or Israeli forces arrested some 50 Palestinian militants under siege in the compound--neither of which happened.</P>
<P>Sharon had hoped the assault--which reduced all but a single building of the large compound to rubble--would quickly get Arafat to surrender.</P>
<P>The opposite happened. "The most important thing is that the Israelis failed to dictate to us," said Nabil Abu Rudaineh, a spokesperson for Arafat. "They wanted to finish President Arafat and to prove that he is irrelevant, and what happened was that he became stronger."</P>
<P>As the standoff wore on, the U.S. government pressured the Israelis to retreat, fearing that a confrontation would undermine U.S. efforts to line up support in the Middle East and Europe for a war on Iraq.</P>
<P>Ephraim Eitam--a leader of the racist National Religious Party and a member of Sharon's cabinet--summed up the "disappointment" felt by right-wing extremists in Israel. "We correctly preferred to give a boost to the matter of an American attack on the Iraqis over something we can always do later," sneered Eitam.</P>
<P>In fact, the Israeli military still hasn't withdrawn from Palestinian cities that it retook in recent months--in violation of a United Nations resolution passed last weekend calling for this in addition to an end to the siege.</P>
<P>As the second anniversary of the Palestinian uprising--or Intifada--passed, Israel's stranglehold on Palestinian life continued, stoking the anger and frustration at Israel's 54-year occupation of Palestinian land.</P>
<P>The uprising began two years ago when Sharon traveled to a Muslim holy site--the Al-Aqsa mosque--in Jerusalem with 1,000 armed security guards. The next day, when Palestinians gathered to protest, then-Prime Minister Ehud Barak dispatched a massive police presence that opened fire on the unarmed crowd, killing several and wounding about 200.</P>
<P>Sharon beat Barak in the 2001 election for prime minister by promising to crack down on the Intifada and "terrorism." But the source of the conflict isn't Palestinian suicide bombers. The fact is that during the years of negotiations following the 1993 Oslo peace accords, Israeli officials continued building settlements on Palestinian land, enforced closures that strangled the Palestinian economy and tried to renegotiate what little they had conceded at Oslo.</P>
<P>Ultimately, Barak's "generous" offer at Camp David two years ago convinced Palestinians that Israel had no intention of allowing a viable and independent Palestinian state. That offer would have confined Palestinians to significantly less than 22 percent of historic Palestine--with even this territory carved up by Israeli settlements and roads--while demanding that Palestinian refugees give up the right to return to their homes.</P>
<P>Until Palestine is a secular, democratic state with rights for both Arabs and Jews, there won't be peace. And as long as the U.S. continues to support Israel to the tune of billions of dollars a year, Israel's terror war on Palestinians will continue.</P>
