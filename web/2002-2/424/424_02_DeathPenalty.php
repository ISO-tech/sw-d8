<P><font face="Times New Roman, Times, serif" size="5"><b>A new blow to machinery of death</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 4, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>FOR THE second time in three months, a federal judge has declared the federal death penalty unconstitutional.</P>
<P>Judge William Sessions ruled that the Federal Death Penalty Act of 1994 can't be reconciled with three recent U.S. Supreme Court rulings intended to protect the rights of capital defendants. The 1994 act expanded the number of federal crimes where a death sentence could be imposed from two to 60.</P>
<P>For now, the ruling only affects the specific case before Sessions, who is a federal district judge in Burlington, Vt. But it adds momentum to the growing clamor against the death penalty. "Capital punishment is under siege," Sessions wrote in his opinion.</P>
<P>In July, Judge Jed Rakoff of the federal district court in Manhattan ruled that the large number of death row prisoners exonerated by DNA and other evidence made capital punishment "tantamount to foreseeable, state-sponsored murder of innocent human beings."</P>
<P>How did the Bush administration react? By attacking the judge as a "liberal" appointed by Bill Clinton. Yet it was Clinton who oversaw the massive expansion of the federal death penalty and new limitations on appeals by death row prisoners.</P>
<P>The death penalty isn't under fire because of Democrats. The execution machine is so obviously broken that public questioning and activism have driven judges to rule against it--and pressured the governors of Illinois and Maryland to declare moratoriums on executions.</P>
<P>Since 1976, some 800 people have been executed--while 100 death row prisoners have been exonerated and freed. All of the people on death row are poor, and they're also disproportionately Black and Latino.</P>
<P>The time to press for abolition is now. No more state-sanctioned murder!</P>

