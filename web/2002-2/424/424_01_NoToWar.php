<P><font face="Times New Roman, Times, serif" size="4">Time to stand up</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>No war on Iraq</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 4, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>FOR WEEKS, George W. Bush has been acting as if the world would simply go along with his demand for a new war on Iraq. But the secret's out. There's opposition to Bush's war drive--and it's growing.</P>
<P>In London, an incredible 400,000 people turned out for an enormous antiwar protest on September 28--showing the depth of opposition to Bush's main accomplice, British Prime Minister Tony Blair. "It's a historic turning point in the European antiwar movement, and therefore it's something that Bush has to deal with," Mike Marqusee, a spokesperson for the Stop the War Coalition in Britain, told <I>Socialist Worker</I>. </P>
<P>That same cause--stopping Washington from organizing another slaughter in Iraq--brought out 150,000 to protest in Rome.</P>
<P>More than 4,000 people marched against the war on September 29 in San Francisco and Washington, D.C., and hundreds turned out in such far-flung cities as Anchorage, Al., and Laramie, Wyo.</P>
<P>The protests in the U.S. aren't as big as in Europe. But they reflect a shift in U.S. politics. A year ago, millions of people in the U.S. were prepared to accept Bush's claim that the September 11 attack required a "war on terror"--starting with a savage bombing campaign against Afghanistan.</P>
<P>Today, Osama bin Laden is still unaccounted for, and Afghanistan is dominated by feuding warlords--while doubts grow about why the Bush gang wants to wage war on Iraq.</P>
<P>Having failed to connect Saddam Hussein to September 11, Bush's pretext for war is Iraq's supposed possession of weapons of mass destruction. Yet rather than take up Iraq's offer of new weapons inspections, Bush and Blair are still braying for war--and pushing for a new United Nations resolution backing military action. </P>
<P>Last week, leading Democrats Al Gore and Ted Kennedy raised questions about the Bush war drive. But we can't trust the Democrats to stop the war on Iraq. We need to build the broadest possible movement to turn doubts and questions into antiwar activity.</P>
<P>Meetings, teach-ins and protests are taking place in scores of communities and campuses across the U.S. And activists are building for the national antiwar demonstration in Washington on October 26.</P>
<P>Let's make that protest as large as possible--and send a message to Bush that we oppose his drive for a new slaughter in Iraq.</P>

