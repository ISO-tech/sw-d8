<P><font face="Arial, Helvetica, sans-serif" size="2"><b>INDIA</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Indians reject the BJP's message of hate</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By David Whitehouse</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 4, 2002 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>INDIA SEEMED poised for a repeat of horrific pogroms against the Muslim minority in the state of Gujarat following a September 24 attack on a Hindu temple. But calls for revenge by Hindu chauvinists went unheeded--in part because of widespread revulsion at attacks last March in Gujarat that left more than 1,000 Muslims dead and another 100,000 homeless.</P>
<P>The crisis began when two Muslim militants took over a Hindu temple in Gandhinagar, the capital of Gujarat. Close to 30 people died by the time Indian commandos killed them. Gujarat's fascist chief minister, Narendra Modi, did his best to provoke a new pogrom. He demanded an invasion of Pakistan, the Muslim-majority country that he blamed for the temple attack. He also described the relief camps set up for homeless Muslims in Gujarat as "child-breeding centers." But at the national level, Modi's party put the brakes on.</P>
<P>The ruling Bharatiya Janata Party (BJP) also blamed Pakistan, but it dispatched thousands of troops to protect Muslims in Gujarat--instead of helping Hindu mobs inflict gruesome terror on them, as Modi's state and local cops had done in March.</P>
<P>The BJP's turnaround came because the massacres last March cost it votes in state elections. But this didn't stop Hindu chauvinist organizations connected to the ruling party from calling for a nationwide strike following the temple assault. The aim was to put violent Hindu mobs on the street--a tactic that sparked anti-Muslim attacks in March.</P>
<P>Even members of the opposition Congress Party got in the act, with the state party backing the call for a strike. The strike was solid in Gujarat and neighboring states, but it didn't lead to the violence that Hindu chauvinist leaders were hoping for.</P>
<P>Meanwhile, more than 1 million Indian and Pakistani soldiers still face each other across the border after 10 months of high tension between the nuclear-armed neighbors.</P>
<P>U.S. officials themselves have stoked this conflict. India has echoed the language of George W. Bush's "war on terrorism" in stepping up its threats against Pakistan. Yet a State Department official last week assured Pakistani officials that Washington would consider their request for help in achieving conventional military parity with Indian forces.</P>
<P>If India and Pakistan are at the nuclear brink, Washington is partly responsible.</P>

