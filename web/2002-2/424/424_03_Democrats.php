<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Democrats' questions stop short of opposition to war</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>All bark and no bite</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 4, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>FOR THE first time in months, leaders of the Democratic Party woke from their collective coma to raise questions about George W. Bush's rush to war.</P>
<P>First, former Vice President Al Gore charged that Bush was putting the "war on terrorism" at risk with his drive for a new war on Iraq. Then, mild-mannered Senate Majority Leader Tom Daschle (D-S.D.) attacked Bush's use of the war as a political stick to beat on the Democrats.</P>
<P>Of course, this is the same Daschle who helped Bush ram the Constitution-shredding USA PATRIOT Act and budget-busting spending increases for the Pentagon through Congress. And Gore, of course, was the junior partner in the presidency of Bill Clinton, which kept up regular bombing campaigns against Iraq--and devastating economic sanctions that are responsible for the death of more than 1 million Iraqis.</P>
<P>Have the Democrats finally grown a backbone? Not really. Congressional Democrats are still likely to vote overwhelmingly for a resolution authorizing Bush's war on Iraq. But the fact that some leading Democrats raised questions about the war marks a shift.</P>
<P>Until Gore's speech and Daschle's outburst, the "strategy" of the Democrats was to duck the issue. They planned a quick vote on Bush's war resolution in order to get back to their campaign focus on "domestic" issues--like the crumbling economy and the health care crisis.</P>
<P>In other words, they wanted to avoid the main issue in U.S. politics until November 5. Naturally, Republicans were able to run circles around them--forcing Democratic leaders to make some response.</P>
<P>With its newly proclaimed "Bush Doctrine" of preemptive war against any country that the U.S. doesn't like, the White House is pushing a radical new departure in U.S. foreign policy. But hardly any Democrat has questioned the U.S. government's right to intervene around the world at will.</P>
<P>That's because Democrats helped to set the precedents that Bush is building on. After all, Bill Clinton dispatched troops in more foreign interventions than the previous three presidents combined. And what was the result of Clinton's interventions in Bosnia and Kosovo if not "regime change"?</P>
<P>As one of the two ruling-class parties in the U.S., Democrats are just as concerned with bolstering U.S. imperialism as Republicans. Their disagreements with Bush are tactical, not fundamental. In fact, most of the criticisms that Al Gore raised about Bush's war drive were similar to those raised over the summer by Republican figures such as former National Security Adviser Brent Scowcroft. But when Washington politicians voice doubts about Bush's war drive, it opens the way for genuine opponents of the war on Iraq to be heard.</P>
<P>Despite Bush's offensive, the public remains highly skeptical of his rush to war. Coupled with increasing evidence that the economy is growing worse, Bush's position is far weaker than it has been since the September 11 attacks.</P>
<P>So far, these doubts haven't been expressed in large-scale antiwar activity. Many people still feel that they are isolated and alone in questioning the Bush war drive--though every opinion poll shows a significant minority of people agree with them.</P>
<P>Gore and the Democrats can't be trusted to oppose a new war on Iraq. But the opening of even a small debate over the war inside the political establishment will give more people the confidence to make their views known.</P>
<P>Our protests can put pressure on all of Washington--Republican and Democrat alike. Bush is finally starting to feel the heat. Fundraising events in Arizona and Colorado--hardly known as hotbeds of radicalism--drew thousands of protesters to challenge the commander-in-thief.</P>
<P>We need more protests, teach-ins and rallies to build an antiwar movement that Washington can't ignore.</P>

