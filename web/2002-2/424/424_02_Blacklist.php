<P><font face="Times New Roman, Times, serif" size="5"><b>Activists targeted by no-fly blacklist</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 4, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WHEN IS a nun considered too dangerous to get on board a plane? When she's a peace activist.</P>
<P>Sister Virgine Lawinger, a 74-year-old Catholic nun from Wisconsin, was one of 20 members of the group Peace Action who were forced to miss a flight to Washington, D.C., in April. Lawinger and the rest of the group were held up because their names were flagged as part of a "no-fly" list compiled by federal agencies.</P>
<P>The list is intended to keep "terrorists" off airplanes. But in the case of the Peace Action group, as well as other antiwar activists, the Feds have clearly been using the list as a database for blacklisting opponents of Bush's war.</P>
<P>According to the <I>San Francisco Chronicle,</I> the CIA, FBI, INS and State Department all contribute names to the list. None of the agencies will explain what criteria they use to add names. They wouldn't explain, for example, why Nancy Oden, a Green Party USA member from Maine, was held up last November when she tried to fly to Chicago.</P>
<P>Oden says that even though she was dressed conservatively and wasn't wearing any antiwar buttons, a National Guardsman seemed to know that she was a peace activist.</P>

