<P><font face="Times New Roman, Times, serif" size="5"><b>U.S. out of the Middle East</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Amanda Maystead and Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 4, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"NO JUSTICE, no peace! U.S. out of the Middle East!" That chant rang out September 28, as more than 4,000 people rallied in San Francisco for a free Palestine and against war on Iraq. The march was sponsored by more than 20 groups, including Al-Awda, Students for Justice in Palestine and the International Socialist Organization (ISO).</P>
<P>Speakers at the rally connected the war abroad to the war facing workers at home. Jack Heyman, of the International Longshore and Warehouse Union (ILWU), announced that the ILWU Local 10 Executive Board passed a resolution in support of Palestine and against war with Iraq. "Labor has no stake in that war," Heyman told the crowd. "We must oppose any war with Iraq!" </P>
<P>"We need a movement to end not just the war with Iraq, not just the brutal occupation of Palestine, but a movement to end all wars!" declared ISO member Snehal Shingavi.</P>
<P>The following day, more than 700 people marched through downtown Chicago as part of the national Al-Awda demonstration to commemorate the second anniversary of the al-Aqsa Intifada. "We have to start to realize that these issues come together, they interlock," Palestinian-rights activist Ahmed Bensouda told <I>Socialist Worker</I>. "Whether it's detentions here, whether it's planned war in Iraq or war in Afghanistan or U.S. support for Israel."</P>
<P>"The 'war on terror'--whether it's against Afghanistan or against Iraq--has nothing to do with fighting terrorism at all," said ISO member Keeanga-Yamahtta Taylor. "On the one hand, it's about oil, and on the other, it's about the U.S. wanting to reassert control over the Middle East and pursue its 'right' to dictate whatever happens around the world."</P>

