<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Bush's union-free Homeland</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 4, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH administration wants to "secure the Homeland." From unions, that is. The White House is insisting that its new Homeland Security department shouldn't have to respect union rights or civil service protections.</P>
<P>The new department will merge 22 already-existing federal agencies and transfer 170,000 employees into one giant Cabinet-level agency. That includes 43,000 workers already covered by union agreements.</P>
<P>If the White House proposal is passed unchanged by Congress, the administration would have freedom to hire, fire, train and reassign employees of the department--and remove workers from unions for reasons of "national security."</P>
<P>The Bush gang claims that it is only eliminating "red tape" that would prevent the new department from protecting Americans from the threat of terrorism. Officials have even used the scandal about pre-September 11 warnings of an impending attack that were missed by the administration as an excuse for pushing the new rules.</P>
<P>But don't let the rhetoric fool you. The Bush gang is out to further its right-wing agenda. These are the people who think that "security" means riding roughshod over civil liberties and rounding up more than 1,000 innocent Arab and Muslim men. These are the people who think that "security" means dropping bombs on Afghanistan and Iraq--regardless of how many people are killed.</P>
<P>Democrats in Congress are opposing Bush's proposal. But their "alternative" isn't much better. Under legislation sponsored by the Democrats, Bush would have to work with unions on personnel matters, but disputes would go to a review board&#133; appointed by Bush. And Bush would still be able to remove Homeland Security workers from unions--as long as he can show that their duties have shifted to work directly related to terrorism.</P>
<P>What would this mean for government workers? For one, the Bush administration could do away with so-called "whistle-blower" protection. That means that government workers who came forward with embarrassing information--like the FBI agents who revealed that the administration had warnings of an impending attack before September 11--could be victimized.</P>
<P>And if the White House can create a new personnel system for the Homeland Security department--eliminating longstanding hiring guidelines and union regulations--other federal departments could follow, including the Defense Department, which has about 600,000 civilian employees.</P>
<P>The Bush proposal for new rules at Homeland Security has nothing to do with security. They're about busting unions and pushing right-wing policies.</P>

