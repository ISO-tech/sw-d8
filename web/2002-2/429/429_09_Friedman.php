<P><font face="Times New Roman, Times, serif" size="4">Thomas Friedman's book on September 11</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>A free-market hack selling a racist war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Adam Turl</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 8, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>BOOKS:</B> Thomas Friedman, <B><I>Longitudes and Attitudes: Exploring the World after September 11,</B></I> Farrar, Straus and Giroux, 2002, 383 pages, $26.</P></font>
<font face="Times New Roman, Times, serif" size="3"><I><P>NEW YORK Times</I> columnist and all-around hack Thomas Friedman was best known prior to September 11, 2001, as a leading cheerleader for globalization--the spread of free-market deregulation and pro-corporate policies around the globe. Since the attacks on the World Trade Center, Friedman has focused on celebrating George W. Bush's "war on terrorism."</P>
<P>A collection of his columns--a stream of racist pro-war venom--makes up most of his new book, <I>Longitudes and Attitudes: Exploring the World after September 11.</I> First of all, Friedman blames just about every problem in the Middle East on Islam: from wars, to poverty and a lack of public education.</P>
<P>Like Bush's talk about "evildoers," Friedman paints a picture of a conflict between the West and the Muslim world in religious terms. "American wealth and power," he writes, "flow directly from a deep spiritual force." The Arab-Muslim world, says Friedman, needs to embrace "American values," jump on the globalization bandwagon, open up its markets and lower oil prices. In other words, poverty and terrorism are caused by a lack of free trade.</P>
<P>Of course, the reality is entirely different, as corporate globalization and U.S. foreign policy have wrecked economies across the world and thrown millions into poverty.</P>
<P>At times, Friedman lets it slip that the "war on terrorism" isn't really about fighting terrorism. Within days of the attack in New York, he wrote that if "the attack on America" is the "equivalent of World War III, it's not too early to begin thinking about what could be it's long-term geopolitical consequences."</P>
<P>So even as rescue workers were still searching for people trapped beneath the rubble of the World Trade Center, Friedman was thinking about how the U.S. could cash in from a new world where, as Bush says "you're either for us or against us."</P>
<P>According to Friedman, the U.S. can do no wrong. "Is it America's fault that the richest ruling family in the world, the [Saudi royal family], have citizens who are poor and frustrated?" he asks. But during the first Gulf War, Friedman himself remarked "the U.S. has not sent troops to the Saudi desert to preserve democratic principles. This is about money, about protecting governments loyal to America and about who will set the price of oil."</P>
<P>Friedman has the gall to assert that the U.S. had nothing to do with the fact that Afghanistan was being run by a "medieval Taliban theocracy" that banned women from working or going to school. The fact is that the U.S., along with Saudi Arabia and Pakistan, encouraged the development of the Taliban--in order to stabilize Afghanistan and acquire access to Caspian Sea oil.</P>
<P>Friedman sees no evil when it comes to the U.S.--but he does think the U.S. has an image problem, especially in the Middle East. So he complains, "It is not an easy trick to loose a PR war to two mass murderers"--meaning Saddam Hussein and Osama bin Laden.</P>
<P>Of course, it is easy if you are an even bigger mass murderer. Friedman barely mentions the 500,000 Iraqi children killed by United Nations sanctions over the past 12 years. He writes off Afghan civilians killed by U.S. bombs as next to irrelevant. And the brutal occupation of Palestine by Israel--underwritten by billions of dollars in U.S. aid--is blamed on Yasser Arafat. </P>
<I><P>Longitudes and Attitudes</I> is a disgusting tome that fronts for a system that means poverty, horror and war for billions around the world. But as Friedman himself wrote in 1998, "The hidden hand of the market will never work without a hidden fist. McDonald's cannot flourish without McDonnell Douglas&#133;the hidden fist that keeps the world safe for Silicon Valley's technology is called the U.S. Army, Air Force, Navy and Marine Corps."</P>
<P>That's what the "war on terrorism" is about--and why U.S. workers have no interest in buying Friedman's lies.</P>

