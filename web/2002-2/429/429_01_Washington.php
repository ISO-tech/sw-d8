<P><font face="Times New Roman, Times, serif" size="4">Billions for Iraq war<br>
Tax cuts for the rich<br>
No help for the jobless</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Washington's twisted priorities</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 8, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>TAX BREAKS for the rich. A relentless drive to war. And crumbs for the rising numbers of long-term unemployed. Those were Washington's priorities before the November 5 elections. And they'll be the priorities the day after.</P>
<P>Already on Election Night, Republicans were bragging about their plans to push more tax cuts now that they control both houses of Congress. Under George W. Bush's $1.35 trillion tax cut package passed last year, the richest 1 percent of taxpayers will rake in 40 percent of the benefits.</P>
<P>A year after the Enron bankruptcy exposed Corporate America's scandalous CEOs, you'd think this outrage would have become the number one issue in the midterm elections. After all, average pay for the top 100 CEOs has soared to $37.5 million--more than 1,000 times the wage of the average worker. </P>
<P>The CEO pay bonanza is part of what <I>New York Times</I> columnist Paul Krugman recently called "the astonishing concentration of income and wealth in just a few hands." But rather than offer any help to the have-nots in U.S. society, the politicians have only helped the rich get richer. </P>
<P>And it isn't just Republicans. Six Senate Democrats provided Bush with his margin of victory on the tax cuts last year. And on the explicit orders of party bosses, Democrats refused to even discuss repealing these giveaways. "Most Democrats are terrified of even confronting the tax problem," <I>Business Week</I> observed last month.</P>
<P>With the most pro-business White House in a generation, the Democrats were in a perfect position to hammer the Republicans in this week's elections. But the "party of the people" was itself too tied to the corporate agenda to provide any alternative.</P>
<P>Meanwhile, congressional Democrats gave Bush a blank check for war in Iraq last month--despite rising opposition to this planned slaughter. If Bush has his way, tens of thousands of Iraqis will be killed in a war for oil and global U.S. domination.</P>
<P>The Democrats have only themselves to blame for their defeat this week. An agenda that reflects the interests of working people simply wasn't on the ballot on November 5. That's why we need to organize--in our workplaces, communities and campuses--and build the resistance to Bush and his policies.</P>

