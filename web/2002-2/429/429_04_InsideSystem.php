<P><font face="Times New Roman, Times, serif" size="4"><b>All the news that's fit to ignore</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SOME REPORTERS just can't count. In covering the October 26 antiwar protest in Washington, D.C., where well over 100,000 people marched, National Public Radio's Nancy Marshall declared, "It was not as large as the organizers of the protest had predicted. They had said there would be 100,000 people here. I'd say there are fewer than 10,000."</P>
<P>When the <I>New York Times</I> first covered the protest in a brief article on October 27, it wrote, "Fewer people attended than organizers had said they hoped for...Participants said the shootings in and around the city in the last three weeks had kept people from planning to visit Washington."</P>
<P>So many people complained that on October 30, the <I>Times </I>ran a "make-up" piece, which gave a more accurate description of the march. The new article mentioned, for example, that marchers formed a "two-mile wall" outside the White House--and that march organizers had taken out a permit for just 20,000 attendees (when five times that many came) and expected just 30 buses (when 650 buses showed up). </P>
<P>--<I>Editor and Publisher</I> <I>Online, </I>October 30, 2002</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Simple Simon runs for governor</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CALIFORNIA'S REPUBLICAN candidate for governor, multi-billionaire Bill Simon, has a full mouth these days. He's been sticking his foot in it constantly.</P>
<P>Simon's campaign against incumbent Gov. Gray Davis has been plagued by his own bumbling--including accusing the governor of sounding like "Karl Marx" when Davis requested that Simon release his tax returns to the public.</P>
<P>Simon's latest blunder came in early October, when he accused Davis of accepting campaign contributions in the governor's office in the state capitol--which is illegal--and provided a picture as proof. But Simon and his flunkies apparently never bothered to look at the picture--which was of Davis accepting a check at a 1998 fundraiser held at a private residence.</P>
<P>When reporters questioned him about it, Simon could only sputter, "I'm not the tribunal here. It's not our job to verify this evidence." And later, Simon refused to apologize, saying: "I don't have any basis right now for an apology."</P>
<P>--<I>San Francisco Chronicle, </I>October 10, 2002</P>

