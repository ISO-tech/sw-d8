<P><font face="Times New Roman, Times, serif" size="4">Opposition to a war on Iraq is spreading</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Stop Bush's war drive!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 8, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SUPPORT FOR a U.S. war against Iraq has continued to drop even as the Bush administration tries to keep up a war frenzy.</P>
<P>"While a 55 percent majority still favors military action, this is fewer than the more than six-in-ten who have consistently supported that option since late August," according to an October 30 report by the Pew Research Center.</P>
<P>"A third (34 percent) now oppose military action, up from 28 percent earlier this month and 21 percent in late August&#133;[And] support for using force against Iraq declines markedly in the absence of allied backing for such an operation."</P>
<P>But none of this matters to the Bush gang. Bush has even begun issuing open threats to the United Nations (UN). If the UN "cannot find the backbone necessary to assume its responsibilities," warned Bush, then the U.S. will assemble its own "coalition" to invade Iraq.</P>
<P>And to make the point crystal clear, Bush dispatched the USS Constellation--an aircraft carrier battle group--from San Diego to the Persian Gulf. In addition, three colossal transport ships--some of the largest in the U.S. Navy--have also set sail for Iraq. The vessels are capable of carrying 58 Abrams battle tanks; 48 track vehicles, such as armored vehicles; and 900 other trucks, according to the Military Sealift Command.</P>
<P>Meanwhile, Navy warplanes patrolling the U.S.-imposed "no-fly zone" over southern Iraq now "have an unadvertised task: practicing bombing runs against Iraqi targets," according to the New York Times. "It gives us the opportunity to train in the same environment that we may possibly go to war in," said Capt. Kevin Albright. "We are looking at target sets and practicing."</P>
<P>In an example of the tortured logic used to defend the escalation of the conflict, Rear Adm. John Kelly said that Iraqi forces were making it harder for allied warplanes to "retaliate"--by minimizing their use of radar! Kelly didn't mention that these "no-fly zones" were imposed unilaterally by the U.S. and Britain--and have no basis in international law or UN resolutions.</P>
<P>But in response to the Iraqi's "evil" plot to make it harder for U.S. jets to "retaliate," Defense Secretary Donald Rumsfeld has given pilots "the authority to attack a broader array of targets," including "an expanded set of command and control centers, communications relay stations, military radars and other stationary targets." In other words, a new war against Iraq is all but declared.</P>
<P>The U.S. has been trying to strong-arm its allies in the Middle East into going along with a war. But so far, not a single one has given its support to an invasion. Last weekend, Prince Saud al-Faisal hinted that Saudi Arabia wouldn't allow Washington to use its territory or airspace to attack Iraq--even with a UN endorsement of military action.</P>
<P>In Turkey, long a staunch U.S. ally, an Islamist party won last week's national elections in a landslide--another blow to Washington's hopes for war allies in the region. With 2 million unemployed and millions more facing desperate poverty, Turkey is one of several powder kegs that could blow up if and when Bush pulls the trigger in Iraq.</P>
<P>No wonder more and more people are questioning the Bush gang's mad drive to war in Iraq. Washington's plans to cause more death and destruction--in a country already devastated by more than a decade of U.S. military and economic warfare--has produced an antiwar movement that has already grown quickly, before a war has even begun.</P>
<P>No war on Iraq! </P>

<P><font face="Times New Roman, Times, serif" size="4"><b>New step in the "war on terror"</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LAST WEEKEND, the CIA launched a missile strike in the countryside of the Arab country of Yemen, killing six men as they drove in a car. The U.S. claimed that one of those killed--Qaed Senyan al-Harthi--was an al-Qaeda operative allegedly involved in the bombing of the USS Cole two years ago.</P>
<P>Officials admit that they're unsure whether al-Harthi played any direct role in the attack on the Cole. But that didn't stop the CIA from making itself judge, jury and executioner.</P>
<P>The U.S. appears to be copying Israel's campaign of "tageted killings" of militant Palestinian leaders--even though these are a clear violation of international law.</P>
<P>Washington is sending a message--that the U.S. will do what it wants, wherever and whenever it wants to.</P>

