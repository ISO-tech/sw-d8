<P><font face="Times New Roman, Times, serif" size="5"><b>Anti-death penalty activist put behind bars</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 8, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Former death row prisoner and anti-death penalty activist Lawrence Hayes is in jail facing parole charges that could land him back in prison for years following a September 24 domestic argument. </P>
<P>Despite the fact that his girlfriend, Annette Pittman, is not pressing charges and is pleading to have Lawrence released from prison, the state is threatening to revoke Lawrence's parole. Lawrence would not be behind bars right now but for that fact that he is still on parole for a wrongful conviction.</P>
<P>Lawrence is a committed and principled opponent of the death penalty. He helped to found the Campaign to End the Death Penalty in 1995, and has worked selflessly with countless organizations in the fight for social justice. </P>
<P>He has long been targeted by the state for his political activities. His original case stemmed from his involvement in the Black Panther Party. Lawrence was convicted in the 1971 shooting death of a police officer and sentenced to die by an all-white, all-male jury. </P>
<P>After spending 20 years in prison, nearly four of those on death row, Lawrence was paroled from prison in 1990. He spent the next eight years as a model parolee, working as a counselor and job trainer for at-risk youth.</P>
<P>In 1998, the day after speaking at a press conference against the death penalty, Lawrence was locked up on trumped up-parole charges. A political and legal campaign won him his freedom after 18 months. </P>
<P>Lawrence is a crucial voice in the fight to end the death penalty. That voice should not be left behind bars.</P>
<B><P>Jackie Hanner, </B>New York City</P>
<I><P>Letters in support of Lawrence can be sent to: Campaign to End the Death Penalty, P.O. Box 25730, Chicago, IL 60625.</P>

