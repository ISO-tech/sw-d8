<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Saying no to a Europe for bosses</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 8, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>TENS OF thousands of people will come this week from across Europe to attend the European Social Forum in Florence, Italy. "A different Europe is possible--against neo-liberalism, war and racism" is the call for the gathering, which will bring together people from a wide range of social movements, unions and political groups to discuss the struggle against the global fat cats and the politicians who do their bidding.</P>
<P>The European Social Forum was called as a follow-up to last year's World Social Forum in Porto Alegre, Brazil, where some 40,000 people came together to discuss how to organize against free-market policies that have crippled the countries of Latin America.</P>
<P>The Florence meeting aims to knit together that opposition across Europe. Dozens of meetings, debates and workshops will take up important questions for the movement--from the U.S. "war on terrorism" to immigrant rights, from Third World debt to the struggle in Palestine. A mass demonstration against the U.S. war drive will cap off the week of events on November 9--with tens of thousands more activists expected for the protest alone.</P>
<P>Until the last minute, Italy's right-wing Prime Minister Silvio Berlusconi had threatened to shut down the forum, citing fear of "violent protesters" on the streets of Florence. Italian officials even tried to suspend Europe-wide treaties and reinstate border controls to keep out activists. Under pressure, Berlusconi had to back down.</P>
<P>In fact, the real threat of violence comes from Berlusconi's paramilitary police force, which gunned down protester Carlo Giuliani at demonstrations against the G-8 summit in Genoa last year. Berlusconi wants to paint protesters as "criminals," but the real crimes are being committed by his government--something that millions of Italian workers who went on a one-day general strike last month to protest government attacks know very well.</P>
<P>The strike demonstrated Italian workers' power to take on Berlusconi and the bosses. And this week in Florence, activists from all over Europe will join them to tell their leaders: No to a world of poverty and war--a better world is possible!</P>

