<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>When the U.S. backs using poison gas</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 8, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"I DO not understand this squeamishness about the use of gas. I am strongly in favor of using poisoned gas against uncivilized tribes." This statement--about gassing Iraq's Kurdish minority--didn't come from that Axis of Evil-doer Saddam Hussein. It came from that icon of Western democracy, Winston Churchill.</P>
<P>Churchill was colonial secretary for the British government, which--along with France--controlled Iraq between 1918 and 1956. As Churchill's statement reveals, the British thought nothing of using phosphorous bombs and other deadly weapons against Kurds and Iraqis in order to put down the struggle for independence.</P>
<P>After the U.S. replaced Britain and France as the leading imperial power in the Middle East, it engineered the coup in 1968 that installed the Ba'ath Party--the ruling party of the Iraqi regime it now seeks to overthrow.</P>
<P>Saddam Hussein's reign of terror didn't bother the U.S. and its allies in the 1980s. They were, in fact, more than happy to supply Saddam's regime with an assortment of chemical and biological weapons--including botulism and anthrax--well after he used mustard and nerve gas in a massacre of Kurdish civilians in March 1988.</P>
<P>After news of the massacre surfaced, the Reagan administration went on record opposing sanctions against Iraq--and actually increased licensing for the sale of chemical-biological agents to Iraq by 50 percent.</P>
<P>A few months after the attack, U.S.-based Bechtel struck a $1 billion deal with Iraq to build a petrochemical plant for the production of mustard gas. Assistant Secretary of State John Kelly, visiting Baghdad in 1989, told Saddam, "You are a force for moderation in the region, and the United States wants to broaden her relationship with Iraq."</P>
<P>Friendly relations with Saddam came to an abrupt halt months later--but not because of Saddam's flagrant human rights abuses, as Bush now claims. In February 1990, Saddam made a speech condemning the massive U.S. military presence in the Gulf--the first indication that he was less subservient to U.S. imperialism than previously assumed.</P>
<P>Saddam warned that, unless its power was checked, the U.S. would dictate the production and price of oil, "all on the basis of a special outlook which has solely to do with U.S. interests and in which no consideration is given to the interests of others." Thus began Saddam Hussein's rapid transformation from a "force for moderation" to "the new Hitler."</P>
<P>Yet a Pentagon draft document leaked after Iraq's invasion of Kuwait in August 1990 demonstrated the accuracy of Saddam's claim. "In the Middle East and Southwest Asia, our overall objective is to remain the predominant outside power in the region," the document states. "As demonstrated by Iraq's invasion of Kuwait, it remains fundamentally important to prevent a hegemon or alignment of [other] powers from dominating the region."</P>
<P>The fact that Saddam "gassed his own people," in other words, played no part in the U.S. government's sudden about-face in 1990.</P>
<P>Meanwhile, just two weeks ago, the Bush administration supported Russian President Vladimir Putin when he gassed his people--killing 120 of 700 hostages held by Chechen rebels and causing permanent brain damage to scores of others.</P>
<P>The U.S. itself has been researching chemical weapons similar to those used in the Russian attack--in apparent violation of the Chemical Weapons Convention, signed by the U.S. in 1997. Scientists at Penn State University are currently developing weapons that can be distributed through drinking water, spray inhalation and "a drug-filled rubber bullet." Potential targets, these scientists say, include hungry refugees who are "agitated and unwilling to wait" for food delivery. </P>
<P>Ed Hammond, director of the Sunshine Project, argues that "incapacitating chemical weapons" such as those used by the Russians "can incapacitate to the point of causing mass death."</P>
<P>By the twisted logic of U.S. imperialism, however, when Washington and its allies use poison gas, they are striking a blow against "terrorism." When its enemies do the same, they are committing crimes against humanity. </P>

