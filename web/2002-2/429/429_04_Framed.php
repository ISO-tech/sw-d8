<P><font face="Times New Roman, Times, serif" size="4">Framed for the Central Park jogger rape case</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>This is a travesty of justice</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 8, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>Kharey Wise lost 13 years of his life for a crime that he did not commit. And then he was repeatedly denied parole because he maintained his innocence. </P>
<P>Wise was one of five young Black and Latino men who were convicted of raping a woman jogging in Central Park in New York City in 1989. Their trial was conducted in a racist atmosphere. The only physical evidence against the five was hairs found on the clothes of the accused men, which prosecutors claimed were from the victim. </P>
<P>But in the trial, prosecutors overstated the reliability of the lab tests that were done, arguing that the hair "matched" the victim, even though the error rates in hair testing are as high as 68 percent.</P>
<P>Sure enough, they were wrong. Recent DNA tests have proven that the hairs did not come from the jogger.</P>
<P>The other pillar of the prosecution's case was videotaped "confessions" that were extorted after a marathon 28-hour police interrogation session, after the five--who were 16 years old at the time--had been separated from their parents. Now, a serial rapist in prison has confessed to the crime.</P>
<P>Because each of the five refused to admit guilt for a crime that they did not commit, they lost their only chance to reduce their prison terms. The mothers of the five have raised a public campaign for justice, including picketing billionaire Donald Trump, who took out a full-page ad in the <I>New York Times</I> in 1989 that advocated the death penalty for the accused rapists. </P>
<P>This kind of racist hysteria was unfortunately followed by mainstream feminist organizations, which argued for tough sentences for the five. As Roger Wareham, the defense attorney for some of the five, argued: "There are many inconsistencies that were pointed out during the trial, but really were ignored because the overriding issue was a white woman had been raped by blacks and Latinos, and there was going to be a conviction."</P>
<B><P>Peter Lamphere,</B> New York City</P>

