<P><font face="Times New Roman, Times, serif" size="5"><b><I>SW</I> distorted Benjamin's views</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 8, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>I have been a subscriber to <I>Socialist Worker</I> and a supporter of the International Socialist Organization for a number of years. I depend on <I>Socialist Worker</I> to provide me with news and perspectives that I cannot get from other news sources. </P>
<P>However, I was disturbed to see you distort the views expressed in Medea Benjamin's recent commentary in <I>USA Today</I> (<I>SW,</I> October 25). You say that she "encourage[s] one form of U.S. military action while opposing another." </P>
<P>But in fact she says that "the new complication in Korea actually points out the absurdity of seeking military solutions." This is consistent with her long-time, highly visible opposition to military action. </P>
<P>When mainstream media distort the news, I know that I just have to see where their money comes from to know why. But when the <I>SW</I> does it, I have to wonder why.</P>
<B><P>Susan Bassein, </B>Davis, Calif.</P>

