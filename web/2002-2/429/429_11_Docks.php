<P><font face="Times New Roman, Times, serif" size="4">Dockworkers and employers reach pact on technology</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Will ILWU deal save jobs?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 8, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>A TENTATIVE agreement on technology in the West Coast dockworkers' contract struggle appeared to give management the upper hand on that crucial issue. Details won't be available until after a full agreement is reached, and negotiations continued as <I>Socialist Worker</I> went to press.</P>
<P>The deal on technology between the International Longshore and Warehouse Union (ILWU) and the employers' Pacific Maritime Association (PMA) came three weeks after George W. Bush invoked the anti-union Taft-Hartley Act that gave a federal judge virtual control of the docks following a 10-day lockout. According to the Associated Press, the union agreed to give up 400 clerks' jobs in exchange for alternative employment for those workers.</P>
<P>The union will also regain control of some outsourced jobs on the docks and the rail yards. Although an ILWU spokesman declared the agreement a "victory," initial press reports indicate that the employers had defeated the union's attempt to win automatic jurisdiction over all new jobs created by the introduction of new technology. Instead, control over the jobs would be decided by arbitration, according to the management magazine <I>The Journal of Commerce</I>.</P>
<P>A hard-line faction in the PMA argued for rejecting the deal because it feared that the arbitration process could give the ILWU too many of the new jobs. To minimize that possibility, the PMA is demanding that the ILWU agree to a new, "neutral" arbitration system.</P>
<P>If the PMA remains aggressive, it's because the employers know that Taft-Hartley wasn't aimed at them, even though the 80-day cooling-off period under the law was used to end their lockout. In reality, the PMA created a crisis so that Bush would be forced to use Taft-Hartley against the ILWU.</P>
<P>Even before the previous contract expired July 1, the administration had threatened to use troops as strikebreakers. Once the PMA engineered the lockout, the White House held a series of meetings with top corporate leaders and lobbyists before Bush invoked Taft-Hartley.</P>
<P>Then, the PMA complained to the Justice Department that the ILWU was engaging in a slowdown--and government officials threatened to take the union to court. If the presiding federal judge was to find that the ILWU had engaged in a slowdown, he could fine the union and jail its leaders if they refuse to pay. Many expected the Justice Department to seek such action after the November 5 elections.</P>
<P>Indeed, the deal on technology reflects the fact that the ILWU was negotiating under coercion. And despite the union's claims of success, the <I>New York Times</I> reported that "one [union] adviser close to the negotiations who insisted that he not be identified, disagreed, saying management obtained almost all of what it wanted on technology, like using optical scanners to speed the entry of trucks carrying cargo." If the technology deal allows them to do so, the ILWU will risk the steady bleeding of membership through outsourcing. Employers have long used such tactics to decimate the membership of unions in the aerospace, steel and automotive industries.</P>
<P>ILWU members have to draw the line. They must be prepared to reject any agreement that could gut their union's power--and mobilize the labor solidarity that's needed to withstand this government-backed assault on our unions.</P>

