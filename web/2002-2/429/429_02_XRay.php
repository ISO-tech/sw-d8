<P><font face="Times New Roman, Times, serif" size="4">Four freed from Camp X-Ray</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>What threat did they represent?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 8, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"THEY KEPT us in cages like animals." That's what Jan Mohammed told reporters last week as he arrived back home in Afghanistan. Mohammed was one of four detainees released last week from the U.S. military's prison camp in Guant&aacute;namo Bay, Cuba--after months of interrogations and brutal conditions.</P>
<P>Mohammed was a farmer forced at gunpoint into the Afghan military by Taliban forces during the U.S. war last year. After he surrendered, he says, soldiers loyal to a U.S.-backed Northern Alliance warlord falsely told American troops that he and other prisoners were Taliban officials. For that, he spent 11 months in Camp X-Ray.</P>
<P>But the Pentagon wasn't offering any apologies. All spokesperson Victoria Clarke would say is that the released men "no longer posed a threat to U.S. security." But what "threat" could Mohammed Hagi Fiz--another released detainee--have posed?</P>
<P>Fiz told reporters that he was 105 years old. "Babbling at times like a child, the partially deaf, shriveled old man was unable to answer simple questions," the <I>New York Times </I>reported. "He struggled to complete sentences and strained to hear words that were shouted at him."</P>
<P>Fiz was held for more than eight months. "My family has no idea where I am, and I've not had any word from them," said Fiz. "I don't even know if they're still alive. All they know is that I went to a doctor and disappeared." </P>
<P>Currently, the U.S. is holding 625 men from 42 countries--with no definite plans to release any more. "There are still many of us left in that prison," said Mohammed. "They think they'll die there."</P>

