<P><font face="Times New Roman, Times, serif" size="4">Denver airport workers arrested in INS raid</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Latest victims of Feds' witch-hunt</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 27, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ATTORNEY GENERAL John Ashcroft's witch-hunters claimed dozens more victims in Denver last week. On September 17, authorities swooped into Denver International Airport with indictments against 110 workers accused of giving fake Social Security numbers on employment applications. </P>
<P>The arrests were part of the Bush administration's ongoing "Operation Safe Sky"--a program of raids at airports across the country supposedly designed to increase security following the September 11 attacks.</P>
<P>So far, more than 650 people have been arrested--yet not a single charge has been connected to terrorism in any way. Most of those arrested are immigrants who may have lied about Social Security numbers in order to get a job. In Denver, the majority of the detainees worked at McDonald's or other restaurants inside the airport.</P>
<P>Yet if convicted, the workers could receive prison sentences of two to five years and fines of up to $250,000.</P>
<P>Don't expect any sympathy from the Bush gang. "We want to take these people to task for their disobeyance of the law," U.S. Attorney John Suthers told the <I>Denver Post</I>. "Secondly, we need to send a message--to let people know that if they try it in the future, they're going to be held accountable." </P>
<P>But when is the Bush administration going to be held accountable for whipping up an anti-immigrant hysteria--all in the name of security?</P>

