<P><font face="Arial, Helvetica, sans-serif" size="2"><b>SOUTH KOREA</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Hospital workers hold out against repression</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sarah Chang</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 27, 2002 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AFTER 119 days on strike, hospital workers at the Kyung-hee Medical Center in Seoul won their fight for a decent wage increase and better conditions.</P>
<P>The walkout there was one of several battles at hospitals that took on national significance recently. Earlier in September, riot police savagely attacked workers occupying Kyung-hee and another hospital, arresting hundreds.</P>
<P>Despite this repression, workers have held out. At Kyung-hee, they won a proposed contract that increases wages by 9 percent, includes partial payment of wages during the strike and improves conditions. But right-wing doctors and the chief of the hospital are threatening not to honor the deal.</P>
<P>The strikes began when union workers from several general hospitals in Seoul and other South Korean cities walked off the job in an illegal strike May 23 after failing to reach an agreement over key demands.</P>
<P>After months of stalled negotiations, 3,000 riot police attacked workers occupying the Kyung-hee hospital and Kang-nam St. Mary hospital on September 11. Women workers were beaten, sexually harassed and forcibly removed from the ground-floor lobbies.</P>
<P>The raid at Kang-nam St. Mary came on the day that workers were due to meet with a Catholic priest from hospital management. Workers later learned that the Catholic association was working hand-in-hand with police--and had signed the orders granting police authority to arrest striking workers who had taken refuge inside the hospital cathedral.</P>
<P>The victory at Kyung-hee should encourage other hospital workers who are continuing their strikes. It has also exposed--once again--the government of South Korean President Kim Dae-jung.</P>
<P>Leaders of South Korean unions are vowing to step up pressure for a settlement at all the hospitals--and to protest new attempts to pass anti-union legislation--in the run-up to presidential elections in November.</P>

