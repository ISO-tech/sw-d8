<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Israeli forces lay siege to Yasser Arafat's compound</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Israel's iron fist</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 27, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ISRAELI FORCES smashed into Yasser Arafat's compound in Ramallah last weekend, using bulldozers to flatten all but one building in which Arafat was trapped. The offensive drew international condemnation--and sparked the largest wave of Palestinian protests in months.</P>
<P>Thousands of Palestinians took to the streets in a half dozen towns--in defiance of Israel's iron-fisted military curfew. In a failed effort to stamp out this latest sign of resistance, Israeli troops fired on the protests, killing at least eight people and wounding dozens.</P>
<P>The government of Israeli Prime Minister Ariel Sharon gave various conflicting reasons for the assault. First, Israeli officials claimed that they were retaliating for two suicide bombings in Israel last week. But Hamas--an Islamic group that is a rival to Arafat's Fatah--claimed responsibility for the attacks. Now, officials claim that they want to arrest "wanted men" holed up with Arafat.</P>
<P>But this is a smokescreen, says Akiva Eldar, a commentator for Israel's <I>Ha'aretz</I> newspaper. "[T]here really is no list of wanted men," wrote Eldar. "We are now demanding that the Palestinians give us a list, from which we will choose the wanted men. From the strategic standpoint, what is underlying the entire&#133;affair is not a decision to put an end to Arafat, but a decision to liquidate the Palestinian Authority [and] put an end to any surviving trace of the Oslo peace process."</P>
<P>Ever since the September 11 attacks last year, Sharon has mirrored the rhetoric of George W. Bush and his "war on terrorism." But the White House's anti-Iraq frenzy in recent weeks gave Sharon room to step up the attack. He isn't prepared to order Arafat's assassination--yet. Instead, Sharon is trying to use terror to shatter Arafat and his supporters, hoping that Arafat will go into "voluntary exile."</P>
<P>Recently, Arafat faced the most serious challenge to his authority in many years--with his own Fatah faction leading a rebellion against him in the Palestinian Legislative Council. But if Sharon thought an attack in Ramallah would be the final blow, he badly miscalculated.</P>
<P>After months in which Israel's barbarous curfew policy had largely forced hundreds of thousands to accept conditions of mass house arrest, the spell was broken. Not only did protesters take to the streets, but they often did so in direct confrontations with Israel's military.</P>
<P>This shows the potential to re-ignite the Palestinian resistance. And that spells trouble for the U.S.</P>
<P>The Bush administration had capitalized on the recent lull in the Israel-Palestinian conflict to push for its war on Iraq. But now the White House is faced with fending off United Nations (UN) resolutions offered by other countries condemning Israel--while trying to get the UN to pass a resolution in support of military action against Iraq.</P>
<P>That's why the U.S. is asking Sharon to pull back, calling his assault on Arafat "not helpful." But Washington hasn't suddenly discovered the justice of the Palestinian struggle. The U.S. stands alone in the world when it comes to its unflinching support for Israel and its dirty colonial war against the Palestinians.</P>
<P>We have to build a fight against Washington's support for Israel's terror--and support the Palestinian resistance.</P>

