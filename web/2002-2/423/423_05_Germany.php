<P><font face="Arial, Helvetica, sans-serif" size="2"><b>GERMANY</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Stage set for new conflicts with Washington</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Schr&ouml;der wins by bashing Bush</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 27, 2002 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>OPPOSITION TO Washington's war drive against Iraq and U.S.-style free-market policies were key to German Chancellor Gerhard Schr&ouml;der's come-from-behind re-election victory last weekend.</P>
<P>In the spring and early summer, opinion polls showed Schr&ouml;der far behind his conservative Christian Democrat opponent Edmund Stoiber. A series of strikes in the metal industry highlighted workers' anger over the government's pro-business policies.</P>
<P>But Schr&ouml;der's response to devastating floods this summer--including his decision to postpone the tax cuts for the bosses--boosted his popularity. He also won support when he denounced George W. Bush's plans for war on Iraq as an "adventure"--and vowed that Germany would oppose military action whether or not the U.S. gets United Nations approval.</P>
<P>The Bush administration denounced Schr&ouml;der's justice minister for saying that Bush was using the war drive to distract attention from economic problems--just as Hitler did in the 1930s. But the charges of "anti-Americanism" only seemed to boost Schr&ouml;der's Social Democratic Party (SPD) and its government coalition partners, the Greens.</P>
<P>Even as the Bush administration stepped up its criticism of him, Schr&ouml;der declared at his final rally: "We say openly: The Middle East, Iraq included, needs a lot of peace, but not a new war."</P>
<P>No one should have any illusions in Schr&ouml;der's commitment to peace, however. His government backed the NATO war over Kosovo--and oversaw the first use of German troops in combat since the end of the Second World War. Schr&ouml;der also committed soldiers to the U.S. war on Afghanistan--and just last week proposed to take the leading role in the occupation of Kabul.</P>
<P>But last November, the SPD-Green government barely survived a no-confidence vote over sending troops to Afghanistan. So during the campaign, Schr&ouml;der decided to play to the antiwar sentiment.</P>
<P>At the same time, his decision to stand up to the U.S. also reflects German capitalism's ambitions. Although Schr&ouml;der failed to make good on his 1998 election promise to reduce unemployment, his criticisms of free-market economic policy this summer shored up his support among workers and unions. He repeatedly railed against the collapse of workers' pensions in the WorldCom and Enron bankruptcies in the U.S.--and vowed never to allow such things in Germany.</P>
<P>But Schr&ouml;der is no enemy of capitalism. Early in his first term, he fired Finance Minister Oskar Lafontaine for advocating higher taxes on businesses and promoting efforts to create jobs through increased government spending.</P>
<P>Now, however, Schr&ouml;der must meet workers' newly raised expectations--and find a solution for an economy that has been growing at a rate of just 0.1 percent a year. Employers are demanding more flexible labor laws to make it easier to carry out layoffs, and European Union rules require a 3 percent budget cut that would mean devastating cuts in social security and welfare.</P>
<P>The unions, by contrast, are vowing to fight to defend their jobs and a progressive social policy. What's more, Stoiber--who turned to racist immigrant bashing in the last days of the campaign--is vowing that the opposition will bring down the SPD-Green coalition inside of a year.</P>
<P>With just a 9-vote majority for the government in the lower house of parliament and a conservative majority in the upper house, the battle over the direction of German society can only grow more intense.</P>

