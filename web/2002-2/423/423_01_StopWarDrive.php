<font face="Times New Roman, Times, serif" size="5"><b>Stop their insane drive to war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 27, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH wants a war on Iraq--and he wants it now. When Iraqi officials agreed to the unconditional return of United Nations (UN) weapons inspectors last week, Bush brushed it aside as the "same old song and dance we've heard for 11 years." But the U.S. isn't really concerned about "weapons of mass destruction."</P>
<P>"The President's appeal to the UN was important from the diplomatic and the military point of view," a senior administration official told Scotland's <I>Sunday Herald</I>. "First, it deflected criticism that the U.S. might be acting unilaterally; second, the timetable gives us greater capacity to get the necessary forces in place in advance of any attack."</P>
<P>Bush and his gang want to get down to the real business--and that's "regime change" in Iraq. The administration summed up its foreign policy goals in a terrifying national security document sent to Congress last week. "The U.S. has long maintained the option of pre-emptive actions to counter a sufficient threat to our national security," the document reads. "The greater the threat, the greater is the risk of inaction--and the more compelling the case for taking anticipatory action to defend ourselves, even if uncertainty remains as to the time and place of the enemy's attack."</P>
<P>In other words, George Bush is claiming the right to attack any country he pleases--and he doesn't need a reason.</P>
<P>The White House echoed this insane "security" strategy in a resolution put before Congress to authorize military action against Iraq. The resolution is so open-ended that it could very well okay the use of force just about anywhere in the Middle East.</P>
<P>But was there any opposition in Washington to this madness? A few members of Congress balked at the wording of the resolution, and others grumbled about the U.S. "acting alone." But there's little debate among Democrats or Republicans about "regime change" in Iraq. They're jumping on Bush's war bandwagon as fast as they can.</P>
<P>House Minority Leader Richard Gephardt (D-Mo.), who voted against the 1991 Gulf War, is certain to join bloodthirsty Democrats like Sen. Joe Lieberman (D-Conn.), who's been gunning for Saddam for years.</P>
<P>The politicians are getting ready to give Bush a blank check for war. We have to expose the Bush gang's real reasons for wanting a war on Iraq--and build the opposition to the never-ending wars that they have planned.</P>

