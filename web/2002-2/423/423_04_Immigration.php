<P><font face="Times New Roman, Times, serif" size="4">Facing deportation after a school trip to N.Y.</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>No human being is illegal</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 27, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>While listening to the radio recently, I heard a story that turned my stomach. In Arizona, the Immigration and Naturalization Service (INS) is threatening to deport a group of teenagers to Mexico. All four are undocumented immigrants, and all have resided in the U.S. since they were toddlers.</P>
<P>Last fall, they were part of a science team that was chosen to attend a national competition in upstate New York to race a solar-powered boat they had built. But when the students tried to cross the border into Canada to do some sightseeing, an INS agent demanded their "papers." When they couldn't produce green cards, they were questioned for more than nine hours by INS agents.</P>
<P>None of the teens knew that they were here illegally. Now, they face deportation. "They are tearing me away from my family," said Luis Nava. "I consider myself an American. I don't know Mexico. And all these hard years I worked in school, just to be kicked out like that."</P>
<P>But immigration officials have no sympathy--and may try to deport the teens as early as this month. "It certainly wasn't something we felt good about, but we can't allow people who we determine are [illegal] to go on their way. That would be derelict in our duty," said one INS official. But lawyer Judy Flanagan has a better way to describe it. "It's racism pure and simple," she said.</P>
<P>The INS blames their "no exceptions allowed" approach in part on heightened security following September 11. But implying that these kids--or any of the thousands of other "illegal" immigrants who toil in low-income jobs everyday across the U.S.--are akin to terrorists is beyond insulting. The system is more than happy to exploit their labor when it's convenient.</P>
<P>There is legislation--called the "Dream Act"--currently pending that would grant undocumented high school students amnesty. But it may not pass in time to help these kids.</P>
<P>We have to fight for a world where no human being is considered "illegal."</P>
<B><P>Nicole Colson,</B> Chicago</P>

