<P><font face="Times New Roman, Times, serif" size="5"><b>Will the "People's Strike" help our fight?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 27, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Global justice activists in the Washington, D.C., area are busy planning for the weekend of September 28, when thousands will come to protest the meetings of the World Bank and International Monetary Fund. Pacifist and antiwar groups are also organizing a march against the impending war on Iraq on September 29.</P>
<P>But there's another element to the weekend--the "People's Strike," organized by the Anti-Capitalist Convergence (ACC) for September 27--that is a step backward for the movement. </P>
<P>The ACC, an anarchist-led group, says that it will "shut down" Washington that day by blocking major highways running in and out of the District. Public literature for the event calls on workers not to go work on Friday--or to give away their products for free, distribute subversive literature or take a long lunch.</P>
<P>But most of D.C. remains unaware of the ACC's plans--let alone their demands. Some in the group say that D.C.'s population shouldn't complain about some disruption--since, after all, we get to live in the capital of the world's richest country. Try telling that to ordinary workers in D.C. who could get fired for missing work.</P>
<P>Many ACC organizers say that they're "bored" with the same old marches and rallies and don't want their action to stifled by cops and permits. In reality, however, the ACC's actions on September 27 will be completely determined by what the police allow them to do--and if events at other recent protests are any indication, they will be arrested quickly.</P>
<P>We are at only the very beginning stages of the kind of struggle that could someday produce real political strikes, led by fighting organizations of workers. </P>
<P>ACC members who genuinely want to build that kind of resistance need to understand that the way to get from here to there isn't adventuristic actions, but building effective, organized events that reach out to a broader audience to build the size and confidence of the movement. </P>
<B><P>Ben Dalbey, </B>Washington, D.C.</P>

