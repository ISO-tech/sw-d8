<P><font face="Times New Roman, Times, serif" size="5"><b>Chronicling the FBI's history of dirty tricks</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 27, 2002 | Page 9</font></P>
<P><font face="Arial, Helvetica, sans-serif" size="2">Ward Churchill and Jim Vander Wall. South End Press, updated editions, 2002.</P>
<B><I><P>Agents of Repression: The FBI's Secret Wars Against the&nbsp;Black Panther Party and the American Indian Movement,</B></I> 550 pages $22</P>
<P><B><I>The COINTELPRO Papers: Documents from the FBI's Secret Wars Against Dissent in the U.S.,</B></I> 500 pages, $22</P>
</font>
<font face="Times New Roman, Times, serif" size="3"><P>ALARMS WENT off for anyone who knows the FBI's sickening history of repression when Attorney General John Ashcroft pressed for more federal law enforcement powers after September 11.</P>
<P>As a contribution to telling that history, South End Press has re-issued two books by Ward Churchill and Jim Vander Wall that provide the details of the FBI's dirty tricks and illegal acts against U.S. activists.</P>
<I><P>The COINTELPRO Papers</I> enumerates the crimes of the FBI's notorious Counter Intelligence Program, or COINTELPRO, as it vied to search and destroy dissent, beginning with what was left of the Communist Party in the 1950s. The book describes how the FBI spied on, infiltrated and led disinformation campaigns to sow division among members and suspicion in the public eye.</P>
<P>The book reprints internal memos to illustrate the lies the bureau cooked up to try to discredit groups like the Socialist Workers Party in the 1940s; the Puerto Rican independence, Black liberation and Native American movements of the 1960s and 1970s; and Central American activists in the 1980s.</P>
<P>"We must seize upon every opportunity to capitalize upon organizational and personal conflicts of the New Left leaders," reads a 1968 FBI office memo. "The creation of factionalism is a potent weapon which must not be overlooked."</P>
<I><P>Agents of Repression</I> focuses on two of the FBI's most brutal and relentless attacks--on the Black Panther Party for Self-Defense and the American Indian Movement. The FBI's more overt attacks, such as the 1973 siege on Native American activists at Wounded Knee, are described in this book.</P>
<P>Neither book can be looked to for broader analysis of political repression and how to respond. And, overflowing with details of brutal attempts to smash the left, these books could leave readers living in Ashcroft's America terrified of what's to come.</P>
<P>But they provide the details of the depths to which our leaders will go to squelch dissent--details they would prefer were locked safely away from the public eye.</P>

