<P><font face="Times New Roman, Times, serif" size="5"><b>The fight for global justice</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 27, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THOUSANDS OF people are headed for Washington, D.C., for a weekend of protests against the International Monetary Fund (IMF) and World Bank.</P>
<P>These terrible twins of international finance are justly hated around the world for imposing misery and suffering--through so-called "structural adjustment" programs that require the governments of poor countries to slash state spending, privatize state-run services and let the free market rip.</P>
<P>The horrible toll of these policies--known as "neoliberalism"--can be seen in every corner of the less-developed world, from Asia to Africa to Latin America.</P>
<P>But neoliberalism and its supporters face a crisis. Mass resistance to the IMF and World Bank has grown steadily in recent years--culminating in the uprising last December in Argentina, where ordinary people rose up to topple a government that was considered the IMF's star pupil. Even establishment voices have begun to question the IMF's free-market medicine--like Joseph Stiglitz, the former World Bank chief economist, in his book <I>Globalization and Its Discontents</I>.</P>
<P>The protests in Washington will be the latest in a series of mobilizations against the IMF and World Bank--and other institutions, like the World Trade Organization and the Group of Eight club of rich industrialized countries. These demonstrations have shaken the corporate globalizers and cast a spotlight on struggles for global justice around the world.</P>
<P>But new challenges lie ahead. Looming over all of them will be the U.S. government's "war on terrorism"--the military face of Washington's drive to dominate the globe. In this special feature, <I>Socialist Worker</I> talks to some of the best-known voices of the movement--about the fight for global justice.</P>

