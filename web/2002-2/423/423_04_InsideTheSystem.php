<P>In particular, Netanyahu recommended <I>Beverly Hills 90210</I> and <I>Melrose Place</I>. "This is pretty subversive stuff," he said. "The kids of Iran would want the nice clothes they see on those shows. They would want the swimming pools and fancy lifestyles."</P>
