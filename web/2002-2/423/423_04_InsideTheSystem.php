<P><font face="Times New Roman, Times, serif" size="4"><b>The vermin of Beverly Hills</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE RATS are descending on Beverly Hills, Calif. A series of mild winters combined with a severe drought has sent the exploding rat population into the homes of the rich and famous to look for food. </P>
<P>Take the case of a well-to-do doctor who recently built himself a dream house off Sunset Boulevard. The doctor was giving a party a few weeks ago when three rats helped themselves to the crumbs off his outdoor buffet table. One scurried in through an open door. </P>
<P>A few days later, he found five rats swimming in his marble pool. "In my pool," he exclaimed to a reporter. "I just built the place. I hear this is happening all over Beverly Hills. Can you keep my name out of the newspaper?"</P>
<P>Rat catchers are reporting an increase in phone calls from panic-stricken socialites. "We'll get hysterical calls about rats crawling on the roofs of houses in Beverly Hills," said Oscar Gonzalez, an exterminator to the stars. "But that's the price you pay for messing with Mother Nature." </P>
<P>--<I>New York Times, </I>September 17, 2002</P>

