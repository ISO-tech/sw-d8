<P><font face="Times New Roman, Times, serif" size="5"><b>Standing up for our right to free speech</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 27, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>On September 14, I was handcuffed and detained for selling <I>Socialist Worker</I> on a public street with five other people. I was singled out by a police officer who demanded that I stop selling the paper and give him my information so that he could write me a ticket. The supposed crime was "vending without a permit."</P>
<P>When I refused to give him my information in protest of the bogus charge, the officer grabbed me by the arm, put me up against store window, handcuffed me and put me in a police cruiser. After I identified myself at the police station, they issued me a $50 ticket. </P>
<P>What makes this so outrageous is that we were exercising our first amendment rights by selling the <I>SW</I>. What's more, the rules are selectively enforced. On that very day, we met a vendor who was not harassed by the police when he failed to show a permit. </P>
<P>This hasn't been the only time that we have been targeted. Four days earlier, at the campus library of the University of Vermont, two armed policemen stopped us from selling the paper until we showed a permit. </P>
<P>Of course, the police knew we had a permit. Nevertheless, because we didn't have it on hand, our paper sale was stalled for 45 minutes. </P>
<P>We have to be clear: these rules are just bureaucratic trip-ups to give cops a reason to target activists. </P>
<B><P>Nathan Moore,</B> Burlington, Vt.</P>

