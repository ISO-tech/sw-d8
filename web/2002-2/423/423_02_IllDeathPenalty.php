<P><font face="Times New Roman, Times, serif" size="5"><b>Will governor in Illinois commute death sentences?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Noreen McNulty</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 27, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ILLINOIS' PRO-CAPITAL PUNISHMENT attorney general filed suit last week to block Gov. George Ryan's case-by-case review of the state's death penalty system. Attorney General Jim Ryan is suing to stop the Prisoner Review Board from holding clemency hearings for death row inmates. </P>
<P>George Ryan, who halted all executions two-and-a-half years ago, has said that he will look into every death row case in the state before leaving office and consider commuting death sentences--possibly a blanket commutation of every prisoner on death row.</P>
<P>But Jim Ryan, the Republican Party's candidate for governor in November, is trying to stop the Prisoner Review Board hearings. He claims that victims' rights aren't being protected.</P>
<P>Yet he certainly doesn't share the same "concern" for victims' rights when it comes to victims of a flawed criminal justice system. In fact, Jim Ryan scrambled into statewide prominence while DuPage County State's Attorney--in part because of his role in prosecuting Rolando Cruz and Alex Hernandez for a notorious crime that they didn't commit.</P>
<P>It took years for Cruz and Hernandez to win their freedom--even after another man confessed to the crime for which they were convicted. Jim Ryan fought their appeals every step of the way.</P>
<P>No wonder he's attacking George Ryan's consideration of a blanket commutation--which would deal a huge blow to the death penalty system that Jim Ryan built his career on.</P>
<P>Sadly, even a few opponents of the death penalty have cautioned that a blanket commutation of all death sentences by the governor would cause a backlash. But George Ryan's moratorium on executions didn't cause a backlash. On the contrary, it cast a spotlight on the injustices of the death penalty system and led more people to question the state's killing machine.</P>
<P>Illinois' death penalty system is too flawed to fix. We have to continue organizing to pressure George Ryan for a blanket commutation--and to build the fight to abolish the death penalty altogether.</P>

