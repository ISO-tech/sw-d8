<P><font face="Times New Roman, Times, serif" size="4">Eric Toussaint:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"The question of war is completely integrated"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 27, 2002 | Pages 6 and 7</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>ERIC TOUSSAINT is the president of the Brussels-based Committee for Cancellation of the Third World Debt and author of <I>Your Money or Your Life: The Tyranny of Global Finance.</P></I>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<B><P>THE IMF and World Bank meetings are taking place amid a crisis of credibility for those institutions. What's your assessment of the situation?</P>
</B><P>A YEAR ago, I gave you an interview just after September 11 where I suggested that there would be a big crisis in Argentina and the question of default on debt would be on the agenda.</P>
<P>Since then, we had combination of two crises--Enron and the corporate scandals, and a crisis in Argentina that has been extended now to all of the region, including Brazil, Uruguay and even Chile.</P>
<P>I would say that now there is a crisis of legitimacy--not just of the institutions, but the neoliberal economic model. Even papers like the <I>Financial Times </I>spoke about the crisis of U.S. capitalism. That's the symptom of a really big change at the world scale. The crisis of the IMF and the World Bank has been extended to the Group of Seven countries as a whole.</P>
<P>There is also a public debate launched by Joseph Stiglitz's book. It's a polemic with the IMF, although what it's saying about the IMF is also valid with respect to the World Bank. Stiglitz's book is a bestseller in France and Spain. </P>
<P>There are reformist currents within the capitalist camp who are trying to launch a debate--something like in the 1930s which says that we need a new deal, some kind of new regulation and a new compromise between the working class and the capitalist class. For me, that is another symptom of the crisis.</P>

<B><P>A SIGN of the debate is that you were recently interviewed in the leading daily newspaper in Argentina, <I>Clar&iacute;n, </I>about the issue forgiving the country's foreign debt.</P>
</B><P>IT'S A conservative daily newspaper with a circulation of 600,000. Giving such a tribune to my opinion is significant. Of course, they don't share it, but they know it's important to give some space to it because it reflects something that masses of people in Argentina want.</P>

<B><P>HOW HAS the U.S. war on Afghanistan--and now the planned U.S. war on Iraq--affected the global justice movement and the resistance to neoliberalism?</P>
</B><P>IN FLORENCE in November, we will have the first European Social Forum, and we expect something like 30,000 to 50,000 delegates. The main issue will be opposition to the war.</P>
<P>We will organize a very big demonstration for November 9--something like 100,000. There are two main goals--the first, trying to impede Bush and Blair from attacking Iraq, and the second, solidarity with the Palestinian people.</P>
<P>At the last meeting of the preparatory committee of the World Social Forum, we published a document against the war, which is circulating widely. The question of the war is completely integrated in the agenda of the antiglobalization movement.</P>

<B><P>MANY FORCES that formerly participated in the U.S. global justice movement--especially the AFL-CIO--say that raising the issue of the war is too controversial. </P>
</B><P>WE DON'T have the same situation in Europe. The trade union leaders know that opposition to the war at the mass level is very important, and they're also trying to link themselves with antiglobalization movement.</P>
<P>For me, it is very clear that the question of the war is the main challenge for the workers' movement. If we enter a situation of permanent war, it would reduce very strongly the possibility of gaining improvements in wages and conditions for the working-class movement. So this pressure from the AFL-CIO and the union leadership in the U.S. is very dangerous for the movement.</P>
<P>The antiglobalization movement has succeeded, since the Group of Eight meeting in Genoa in 2001, to more or less disorganize meetings of the IMF, World Bank the World Trade Organization, etc.</P>
<P>After September 11, there has been a strengthening of the mobilization on the world scale--with the exception of the U.S. We had several hundred thousand demonstrate in Barcelona against the European Union in March.</P>
<P>And the World Bank, IMF, WTO and G-8 have only succeeded in organizing meetings without being disrupted by demonstrators by holding them in places like Doha, Qatar, and Kananaskis in the mountains of Canada.</P>
<P>The perspective of what will happen in the U.S. is of concern. But what happens there is also the hope of the antiglobalization movement's capacity to grow.</P>

