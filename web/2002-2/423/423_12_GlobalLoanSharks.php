<P><font face="Times New Roman, Times, serif" size="4">Rich nations squeeze the world's poor</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Stop the global loan sharks</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 27, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE ECONOMIC and political leaders who will gather for the annual meetings of the International Monetary Fund (IMF) and World Bank in Washington, D.C., this week will talk about poverty. They'll talk about "strategies" to solve world hunger and to halt the spread of AIDS in Africa.</P>
<P>But talk is cheap. And so are they. Beneath the rhetoric, their policies will be directed towards one goal--squeezing working and poor people across the globe for the benefit of the world's most powerful corporations.</P>
<P>The IMF and World Bank's war on the world's poor has contributed to the growing gap between rich and poor around the world. According to the United Nation's (UN) latest Human Development Report released this summer, 52 countries--more than one-quarter of the total--ended the 1990s poorer than at the beginning of the decade.</P>
<P>Overall, 2.8 billion people still live on less than $2 a day. Meanwhile, the richest 1 percent of the world's population rakes in as much income each year as the poorest 57 percent.</P>
<P>And the response of the governments of the developed world? Cut economic aid to poor countries. For Africa, economic aid was halved in real terms over the 1990s, from $39 per capita to $19 at the end of the decade. </P>
<P>"Globalization is forging greater interdependence, yet the world seems more fragmented--between the rich and the poor, between the powerful and powerless, between those who welcome the new global economy and those who demand a different course," the UN's report admitted.</P>
<P>As Mark Mallock Brown, a UN Development Project administrator, said: "We live in a world economy that on nearly every important measure remains firmly tilted against developing countries&#133;And exacerbating matters, many key institutions--from the World Bank to the IMF to the UN Security Council--are not sufficiently democratic or transparent."</P>
<P>That's putting it mildly. The "aid" to poor countries from the IMF and World Bank has always had strings attached--requirements for "structural adjustment" programs that force poor countries to open up their markets to foreign investors while privatizing state-run services and slashing government spending.</P>
<P>It's a recipe for driving masses of people deeper into poverty and despair. In fact, a World Bank report released last week admits that--fully two years after Western leaders agreed to a "rescue plan" for the 40 countries that owed the most money to Western banks and institutions--more than half are being strangled by unsustainable debts. </P>
<P>The World Bank estimates that combating AIDS will cost low-income countries at least 1 to 2 percent of their gross domestic product. Yet 13 of the 26 countries receiving "debt relief" are spending more on servicing debt than on public health.</P>
<P>The governments of developed countries could lessen suffering around the globe and prevent thousands of deaths that are caused by poverty everyday--simply by canceling the debts of Third World countries.</P>
<P>But for them, money is more important than human lives. And Washington is leading the way. The Bush administration's new National Security Strategy turned over to Congress last week revolves around waging war on the world--and that means forcing "free trade" down the throats of poor countries and crushing any that step out of line. What the Bush gang means is simple: What we say goes, economically or militarily.</P>
<P>People around the world are fed up with the U.S. government's drive for global dominance--especially the part played by Washington's servants at the IMF and World Bank. This weekend, global justice activists in the U.S. will have a chance to send a message that we're fed up with their free-market "solutions" to poverty. Thousands of people will attend a week of teach-ins and demonstrations.</P>
<P>"[P]ost-Enron, it's hard to believe that companies can be trusted to keep their own books, let alone save the world," activist Naomi Klein recently wrote. "And unlike a decade ago, the economic model of laissez-faire development is being rejected by popular movements around the world."</P>
<P>The demonstrations this week can show the IMF and World Bank that our world is not for sale--and that we'll stand up against corporate greed.</P>
