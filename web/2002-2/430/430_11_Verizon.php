<P><font face="Times New Roman, Times, serif" size="5"><b>Verizon</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By CWA shop stewards</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 15, 2002 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--The fight against layoffs at Verizon is entering a new stage as the company prepares to axe as many as 3,000 low-seniority workers, possibly before the holidays.</P>
<P>The Communications Workers of America (CWA) and the International Brotherhood of Electrical Workers (IBEW) have begun a public relations campaign to expose Verizon's dirty laundry and save members' jobs. </P>
<P>Verizon claims that the crisis in the telecommunications industry is sufficient justification for layoffs, even though its infrastructure is in desperate need of repair. But in its announcements to Wall Street, Verizon claims to have made $2.1 billion in profits last quarter.</P>
<P>Bad investments and unrealistic profit forecasts did cause Verizon's stock price to fall this year. But the top nine executives aren't penalizing themselves--they made $395 million over the last five years.</P>
<P>CWA and the IBEW have embarked on a campaign to fight the layoffs, focusing on the greed of company executives and the decrepit condition of phone cable. The unions are also fighting court battles to challenge the company's contention that it has a contractual right to lay off workers.</P>
<P>But this strategy falls short. If judges or arbitrators rule in the company's favor, thousands of union members will lose their jobs for the first time in New York phone company history. And all the public sympathy in the world won't stop layoffs if Verizon workers aren't mobilized to defend themselves.</P>
<P>Most Verizon workers already know this, and a number of us have argued for action at local meetings. As a result, debates have opened up within the unions' leadership as well as among the rank and file over possible strike action.</P>
<P>An important solidarity rally is planned for November 20 at Ground Zero. But the union needs to coordinate actions in every work gang, on every floor or with every work unit. A unified work-to-rule campaign is also crucial to push back against the company as it seeks to discipline workers for the most minor infractions. The company can defeat isolated job actions, but not a unified one. </P>
<P>Verizon workers are facing the most serious test of our strength ever. If Verizon gets away with this first round of layoffs, they will demand more--just like AT&amp;T, Lucent and virtually all other telecommunications companies.</P>
<P>The CWA won quick strikes in 1998 and 2000 as well as long ones in 1971 and 1989. We can do it again.</P>

