<P><font face="Times New Roman, Times, serif" size="4">Mentally ill and on death row</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Sick face of Texas killing machine</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 15, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE HORROR of Texas' killing machine was exposed again last week. After a last-minute stay of execution, death row inmate James Colburn was spared from being put to death for the 1994 murder of Peggy Murphy.</P>
<P>Colburn admits that he committed the crime. But in a just world, he would be receiving treatment in a hospital--not languishing behind bars and waiting to die.</P>
<P>James was diagnosed at age 14 with paranoid schizophrenia. At 17, he was raped, and soon after, his schizophrenia became more severe. He began hearing voices--which, according to his medical records, often told him to kill himself or his family--and suffering delusions. James has tried to commit suicide at least 15 times.</P>
<P>Throughout his life, his family--and James himself--tried to have him committed to public health facilities. But because James comes from a poor family--and because it's not "profitable" to care for people like him--he was given minimal treatment and usually discharged from these facilities with nothing more than a bottle of pills.</P>
<P>His family said that he came to feel secure in prison, where his medication is monitored and his freedoms are restricted. He told investigators that one reason he committed murder was so he could return to a cell.</P>
<P>Yet despite all this, a state psychiatrist found James competent to stand trial. An appeals court later ruled that it didn't matter that James had slept through parts of his trial--noting that it was uncertain how often he had slept and that his lawyers had failed to "show his sleepiness rendered him unable to understand the trial proceedings or to assist his attorneys in his defense."</P>
<P>At least two jurors have come forward to say that if they had known everything about James, they would not have voted for the death penalty. But Texas officials are pushing for this man to be executed--at any cost. And in the sickest twist of all in this sick story, James has been sent for repeated psychiatric care while on death row--so that he will be "well enough" to be executed.</P>
<P>While James' life has been spared temporarily, his case shows how barbaric the death penalty system truly is.</P>

