<P><font face="Times New Roman, Times, serif" size="4">Chicago cops intimidate global justice rally</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Daley's protest hysteria</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sherry Wolf</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 15, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>DID THEY really expect an amphibious assault on corporate executives? Chicago police had a gunship docked on the Chicago River to reinforce thousands of cops, police cavalry and a fleet of helicopters in protecting a meeting of U.S. and European business leaders last week.</P>
<P>The security crackdown was incredible. Decked out in their new kevlar-coated armor and riot gear, the cops surrounded more than 2,000 peaceful labor and student protesters, who marched and chanted in a permitted demonstration against the Trans-Atlantic Business Dialogue (TABD).</P>
<P>The TABD meets in secret to draw up recommendations for eliminating transatlantic trade and labor regulations, which they then pass on to the World Trade Organization. While the war drums grow louder on both sides of the Atlantic, this year's TABD meeting was co-chaired by military and commercial aerospace giant Boeing and British-based BAE Systems, which makes technology for warplanes and weapon systems.</P>
<P>The irony of martial law-type measures used against activists to protect the warmongers scheming inside the posh Chicago hotel wasn't lost on protesters. Striking workers from the Azteca tortilla factory and grocery workers facing a possible strike or lockout at Dominick's addressed the crowd about the violence of corporate greed. </P>
<P>The thousands-strong turnout was especially inspiring after the weeks of media lies and Mayor Richard Daley's threats to fine activists for any property damage. Aided by the Chicago press, Daley whipped up a frenzy about the coming protest--and used the participation of a handful of local anarchists in organizing for the demonstration as an excuse purchasing millions of dollars worth of new equipment for cops.</P>
<P>The city has refused so far to release figures for the cost of the three-day occupation of Chicago's downtown--and no wonder. Daley and the cops tried to demonize the global justice movement. Instead, they came away looking like the fools that they are.</P>

