<P><font face="Times New Roman, Times, serif" size="4"><b>Milking an ad for all its worth</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE FAMOUS "Got milk?" advertising campaign just hit a new low. Last month, the California Milk Processor Board--struggling to stabilize declining and flat milk sales in the nation's leading dairy state--offered 24 small California towns the chance to win some cash, free school computers and a library expansion or new playground.</P>
<P>With one small catch. The winning town would have to change its name to "Got Milk?" Jeff Manning, the milk board's director, said all he wants is to "pick up a newly printed California map and run my finger down a road and see Got Milk? California." </P>
<P>The only town to briefly consider the idea was the Central Valley town of Biggs, which has 1,793 residents. But many in Biggs--which is an hour's drive north of Sacramento--believe becoming Got Milk? Calif. will make them a laughingstock. "We'll get made fun of all the time," said high school student Laura Rodriguez. "Where you from? We're from Got Milk? They'll say, 'Here come the cows.'" </P>
<P>It wouldn't be the first time a town changed its name for profit. In 2000, Halfway, Ore., renamed itself for one year to Half.com to promote an e-commerce Internet startup. "There's a couple of signs that say, 'Welcome to Half.com, America's first Internet city,'" said Steve Backstrom of the <I>Hells Canyon Journal</I>. "Other than a couple of film crews and journalists that came in, I wouldn't say we saw a boost in tourism from that." </P>
<P>--Associated Press, November 1, 2002</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Tapped for an odd phone bill</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AN INTELLIGENCE-gathering operation in Germany went spectacularly wrong when bills for phone tapping were sent to the people who were being bugged.</P>
<P>About 50 people received demands for payment in their mobile phone invoices. But when they called a mysterious number listed on their bills, it turned out to be the number of a recording device used by the intelligence service.</P>
<P>German federal police last week sent an urgent warning to all regional police forces, who are checking records to see who received the bills and whether they were aware of the eavesdropping. </P>
<P>--<I>The Telegraph,</I> November 2, 2002</P>

