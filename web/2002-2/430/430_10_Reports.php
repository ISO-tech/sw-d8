<p><a name="gay"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend gay rights</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Martin Smith</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SAN FRANCISCO--Gay rights activists in the Bay Area are preparing for the visit of hate preacher Fred Phelps later this month. Phelps is best known for protesting the funeral of Matthew Shepard, the victim of a brutal hate crime outside Laramie, Wyo., in 1998--with signs that read "God hates fags" and "Matthew burns in hell." </P>
<P>Phelps is coming to the Bay Area in the wake of the recent murder of 17-year-old transgendered student Gwen Araujo. On November 5, more than 40 students at the University of California-Santa Cruz (UCSC) held a rally against hate and to organize an emergency response team to demonstrate against Phelps if he should make an appearance on campus. </P>
<P>Hate crimes and gay bashing often increase when homophobic bigots like Phelps visit a given area. That's why it's critical to organize large numbers against Phelps' bigotry.</P>
<I><P>For more information about UCSC demonstrations against Phelps, e-mail nohate@ucsc.edu.</P></I>

