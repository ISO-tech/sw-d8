<p><a name="CEDP"></a><font face="Times New Roman, Times, serif" size="4"><b>Campaign to End the Death Penalty</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--More than 100 people attended the second annual convention of the Campaign to End the Death Penalty last weekend. The event brought together activists from across the country to plan strategies for the anti-death penalty movement at this critical time.</P>
<P>The convention acknowledged that the sniper shootings in the Washington, D.C., area and the clemency hearings in Illinois have put some wind in the sails of pro-death penalty forces. But in the last year, there have also been a string of victories for the abolitionist movement--including a moratorium on executions in Maryland and a string of court decisions on a range of issues.</P>
<P>Keynote speaker Stephen Bright of the Southern Center for Human Rights laid out in sharp detail the injustices and barbarism of the death penalty while Marlene Martin, the Campaign's national director, addressed the state of the abolitionist movement today.</P>
<P>And a plenary session called "The Face of Discrimination in the Criminal Justice System" brought together exonerated prisoners who put a human face on the racism at the heart of the death penalty system. The panel was made up of Angola Three member Robert Wilkerson, who spent 29 years in solitary confinement; Shujaa Graham, an exonerated California death row prisoner and former Black Panther; Angel Rodriguez, from the Comit&eacute; Exegimos Justicia, who spent four years in prison before being exonerated; and Larry Ollins and Omar Saunders, two of four young, Black men who spent nearly 15 years in prison for a murder they didn't commit.</P>

