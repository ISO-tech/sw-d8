<P><font face="Times New Roman, Times, serif" size="4"><b>Howard Zinn on war and terrorism</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>EVANSTON, Ill.--More than 600 people turned out to hear radical historian and activist Howard Zinn speak at Northwestern University November 6. Audience members packed the auditorium, sitting in the aisles and standing along the walls.</P>
<P>Speaking on "History, War and Terrorism," Zinn challenged the idea that the U.S. government has ever been "of and for the people." "We've been a class society from the beginning, from before the Constitution," he told the crowd. "Trace the history of legislation in the U.S., and it's a history of class legislation&#133;only interrupted when there were major movements in the 1930s and 1960s."</P>
<P>Zinn's message was that the only way to win real gains and stop a new war on Iraq is to fight and struggle to build a movement from the bottom-up. "If people continue, if people persist, if the movement grows, who knows? Things change because of social movements, not because of edicts from on high&#133;It's possible for things to change if people persist," said Zinn.</P>

