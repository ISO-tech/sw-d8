<p><a name="SOA"></a><font face="Times New Roman, Times, serif" size="4"><b>School of the Americas</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Russell Pryor</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>COLUMBUS, Ga.--Just miles from Ft. Benning, which houses the infamous School of the Americas (SOA), a historic debate took place between Father Roy Bourgeois, founder of SOA Watch, and Col. Richard Downie, the SOA's commandant.</P>
<P>The SOA--a military training school for Latin American soldiers--has pumped out death squad leaders and dictators trained to terrorize peasants, workers and the poor. Today, the SOA continues business under its new name--the Western Hemisphere Institute for Security Cooperation.</P>
<P>Col. Downie opened the debate with the jaw-dropping assertion that the SOA and SOA Watch were both dedicated to promoting the welfare and rights of the workers and peasants of Latin America and that both had a deep concern for democracy. Had anything he said been true, the debate would have ended there. </P>
<P>There was one simple thing that he didn't deal with in anything that he said--reality. When it came time for questions from the audience, Father Roy did an excellent job, but Col. Downie also dodged every question, refusing to give straight answers.</P>

