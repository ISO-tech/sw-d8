<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Why the UN voted for war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 15, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE UNITED Nations (UN) Security Council set the stage last week for a war on Iraq. By a unanimous vote, the 15 countries of the Security Council gave the Bush White House just about everything it wanted--a resolution requiring Iraq to comply with incredibly intrusive weapons inspections and allowing the slightest incident to be the grounds for war.</P>
<P>For example, if Iraq uses its radar to track a U.S. plane in Iraqi airspace, this could constitute a "hostile act" against a member state upholding UN rules--and amount to a "material breach" of the resolution.</P>
<P>But even without such an incident, the U.S. has no intention of allowing Saddam Hussein to "comply" with inspections. In fact, full compliance is "the nightmare scenario: Saddam plays along and manages to seem to get a clean bill of health," an unnamed Pentagon official told the <I>Chicago Tribune</I> last weekend. "Then how do you show that an invasion would be warranted?"</P>
<P>The U.S. got what it wanted using the carrot and the stick. With France and Russia--two permanent members of the council that theoretically could have vetoed the resolution--the U.S. promised a cut of oil revenues in a post-Saddam Iraq. The two countries have billions of dollars in oil contracts with Iraq that they feared would be terminated after "regime change."</P>
<P>With other countries, the U.S. generally used the stick. For the eight countries that receive U.S. economic aid, there was the example of Yemen. Before the 1991 Gulf War, Yemen cast the lone dissenting vote against a UN resolution authorizing military action. Moments after the vote, a U.S. official appeared beside Yemen's representative and told him bluntly, "That's the most expensive 'no' vote you'll ever cast." Three days later, the U.S. cut its entire aid budget of $70 million to Yemen, the poorest country in the Middle East.</P>
<P>Then there's Syria. With their country on the U.S. list of "terrorist states," Syrian officials clearly concluded that a "no" vote could have made them "a tempting target for the kind of treatment Iraq has been accorded," as the <I>New York Times</I> diplomatically put it.</P>
<P>For antiwar activists--especially those who hoped that UN weapons inspections would be an alternative to war--these events hold an important lesson. The UN has always been a tool for the world's most powerful countries--the U.S. chief among them. Most of the time, Washington can get the UN to go along and give a veneer of international legitimacy to its wars. But when it runs into obstacles, the U.S. can simply disregard the UN.</P>
<P>The antiwar movement won't get anywhere calling on the U.S. to obey the UN or international law. Our power lies in mobilizing the largest possible opposition to Bush's war.</P>

