<P><font face="Times New Roman, Times, serif" size="5"><b>Antiwar veterans get cheers on Veterans Day</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 15, 2002 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>AT THIS year's Veterans Day Parade in New York City, the cheers went to a group of 50 antiwar veterans carrying a huge "Veterans for Peace" banner. They also had signs like "Bush's war is bullshit" and "Say no to Bush's war on Iraq."</P>
<P>Throughout the march, the antiwar vets were welcomed with applause, cheers and shouts of "Thank you" and "Thank God you're here" from the crowds lining the parade route. Proudly marching in a traditional military column of fours, the Vets chanted, "One two three four, we don't want another war! That is what we're marching for!"</P>
<P>The day before, a crowd of 150 showed up at a Veterans for Peace speak-out to organize resistance to any attack on Iraq--the largest meeting of antiwar vets in New York in 30 years. "Everybody knows it's just about oil," 19-year-old Pvt. Wilfredo Torres told <I>Socialist Worker</I>. Torres was the guest of honor at the event, explaining how he joined the Army to get cooks training and that he will refuse to fight in Bush's war.</P>
<P>During the open mike, speakers called on vets to reach out to men and women in the armed services today with arguments against the war and support for their resistance. The meeting also marked the debut of Veterans for Common Sense, a new organization of Gulf War veterans organized by Charles Sheehan-Miles. He told the crowd how he and other Gulf War soldiers were ordered to massacre a group of Iraqi soldiers who were on fire from exploding gasoline.</P>
<P>Meanwhile, on campuses across the U.S., students were organizing protests and meetings to oppose war on Iraq. At the University of California-Berkeley, the Berkeley Stop the War Coalition is planning to protest a November 19 speech by former Israeli Prime Minister Ehud Barak. Barak has been touring U.S. campuses in recent weeks speaking on the topic of "peacemaking"--a joke considering that Barak has repeatedly stressed that he supports a unilateral U.S. military attack on Iraq.</P>
<P>In Chicago, a few dozen students from 10 Chicago-area schools have been meeting as the Chicagoland Student Anti-War Network (CSAWN) to coordinate antiwar activity among students. CSAWN's first event--a rally in downtown Chicago--will take place November 20. CSAWN brings together organizers from antiwar committees set up at high schools, city colleges and universities throughout the area.</P>
<I><P>Thomas Barton and Sid Patel contributed to this report.</P>

