<P><font face="Times New Roman, Times, serif" size="5"><b>Antiwar movement needs to be able to have debates</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 15, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>Susan Bassein takes issue with what she sees as <I>Socialist Worker</I>'s distortion of Medea Benjamin's recent <I>USA Today</I> editorial (<I>SW,</I> November 8). </P>
<P>In late October, <I>SW</I> editors wrote that Benjamin "encourages one form of military action while opposing another." Bassein correctly points out that Benjamin opposes unilateral U.S. military action against North Korea--and that she has been leading protests against a new war against Iraq. </P>
<P>However, there is no other way to interpret Benjamin's assertion that "our goal right now should be to break up the terrorist network that attacked us on September 11, not be the unilateral global vigilante," than as conditional support for some type of U.S. military action.</P>
<P>Benjamin argues in the same editorial that "regional pressure" should be exerted on North Korea to disarm. In a country that is still divided and occupied by U.S. troops, ringed by dozens of nearby U.S. military bases and the 7th Fleet, and the site of an escalating U.S. military buildup, calling for "regional pressure" makes it seem like U.S. "diplomatic pressure" can be separated from the threat of U.S. military action. </P>
<P>It cannot. <I>SW</I> has consistently argued against the idea that the U.S. government and military can play any progressive role, anywhere in the world. Martin Luther King once pointed out that the U.S. government is the "greatest purveyor of violence in the world."</P>
<I><P>SW</I> argues, I think correctly, that antiwar activists must understand that our job lies only in stopping our government's ability to wage any kind of war, no matter the excuse--not to advise it about how to more humanely pursue "terrorist networks."</P>
<P>Many honest and excellent antiwar activists, including Benjamin, disagree with <I>SW</I> on this point. Making that clear is not a distortion. It is a debate. And an important one at that.</P>
<B><P>Todd Chretien,</B> Oakland, Calif.</P>

