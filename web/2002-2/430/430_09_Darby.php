<P><font face="Times New Roman, Times, serif" size="5"><b>Describing a journey from death to life</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Julien Ball</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 15, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>MUSIC: <I>Death to Life,</B></I> by Darby Tillis, Just Us Productions, 2002, $10.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>On <I>DEATH to Life,</I> Darby Tillis delivers a highly personal, four-song blues and gospel CD that chronicles his wrongful death sentence and the spiritual awakening that allowed him to cope with his incarceration.</P>
<P>Now an ordained minister, Tillis spent more than nine years on Illinois death row after he was wrongfully convicted in 1977.</P>
<P>Tillis and his co-defendant Perry Cobb, who are both Black, were sentenced to death by an all-white jury. Their trial judge, Thomas Maloney, has since been sentenced to 15 years in prison for accepting bribes in capital cases. Later, Phyllis Santini, a woman who implicated Cobb and Tillis, admitted to an assistant state's attorney that her boyfriend was guilty of the murder.</P>
<P>In the title track, "Death to Life," Tillis doesn't sing. Instead, he eloquently recounts his story and tells of the barbarity of the death penalty. "Whether a death sentence is carried out in six minutes, six weeks or six years, the person set for death begins to suffer the most cruel, inhuman and degrading treatment and punishment. Death row is segregated from the rest of the general inmate prison population. You're warehoused for death&#133;treated like contaminated meat to be disposed of."</P>
<P>In standard blues style, "It Wasn't My Fault" documents the difficulties Tillis faced in restarting a life after his release. And on "Preaching to the Row," Tillis stresses the pain of life on death row--and how religion eased that pain.</P>
<P>The strength of this album is Tillis' soul--which shines through whether he's telling his story about survival on death row, preaching the gospel or letting loose on the harmonica.</P>
