<P><font face="Times New Roman, Times, serif" size="4">Thousands build for School of the Americas protest</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The terrorists in Bush's backyard</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 15, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AS WASHINGTON gets ready for the next stop in its "war on terrorism," there's one question that you can be sure the Bush administration won't answer. What about the terrorists in their own backyard--the ones trained by the U.S. military?</P>
<P>Since 1945, the School of the Americas (SOA) in Fort Benning, Ga., has been a training ground for more than 60,000 Latin American military personnel, including some of the most notorious butchers and dictators in Latin Americas. "It is hard to think of a coup or human rights outrage that has occurred in [Latin America] in the past 40 years in which alumni of the School of the Americas were not involved," the <I>Los Angeles Times</I> has written.</P>
<P>When SOA training manuals were exposed in 1996, the <I>New York Times</I> reported that they "recommended interrogation techniques like torture, execution, blackmail and arresting the relatives of those being questioned."</P>
<P>U.S. officials proudly declare that 10 Latin American presidents have graduated from the SOA. But each and every one took office by illegal means, such as a coup.</P>
<P>In April, SOA "alumni" led the failed coup in Venezuela--after meeting with Otto Reich, Bush's Assistant Secretary of State for Western Hemisphere Affairs, who sits on the SOA's monitoring board. </P>
<P>Since the early 1990s, activists in the group SOA Watch have organized to get the school shut down, holding an annual demonstration at Fort Benning that draws thousands. Under pressure from activists, Congress two years ago renamed the school the Western Hemisphere Institute for Security Cooperation.</P>
<P>But as SOA Watch activists point out, WHISC may be the new name, but it's the same shame. That's why protesters will go to Fort Benning for the annual protest against the SOA on November 15-17.</P>
<P>This year, many protesters are drawing the connection between the fight at home and the struggle against the Bush gang's war drive against Iraq. "Iraq is dominating the news and it's bringing more people into our movement," Father Roy Bourgeois, a founder of SOA Watch, recently told the <I>Savannah Morning News</I>.</P>
<P>"We want to shut down this school, and we will. But it's bigger than this school. We are also out to change this country's foreign policy. This school is an expression of that."</P>

