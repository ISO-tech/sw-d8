<P><font face="Times New Roman, Times, serif" size="5"><b>Why children starve to death in a world of plenty</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 15, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GOVERNMENTS IN the world's richest countries are letting millions of people starve to death, according to a report by the United Nations (UN) Commission on Human Rights.</P>
<P>Every year, the report says, lack of food claims nearly 36 million lives. "Every seven seconds, a child under 10 years of age dies from the direct or indirect effects of hunger," Jean Siegler, special rapporteur of the commission told InterPress Service.</P>
<P>The real problem, according to the UN, isn't a lack of food or resources. It's a lack of promised action by governments of the advanced world.</P>
<P>Some 815 million people go hungry around the world today, according to the UN Food and Agriculture Organization. In many countries, particularly in Southern Africa, the situation is deteriorating, not improving. Yet while the World Food Program appealed for $611 million in food and non-food support for southern Africa in mid-July, Western countries have promised only about one-third of that amount.</P>
<P>Siegler pointed to "profound contradictions" in the actions of some states that claim free trade in agriculture is the way to reduce hunger--at the same time as they protect their own economies with huge agricultural subsidies. </P>
<P>Earlier this year, for example, the U.S. announced a package that would increase agricultural subsidies by $180 billion over the next 10 years. "It is clear that these actions contribute to the profound inequities within the current international trading system, with severe impacts on the realization of the right to food, particularly in developing countries," Siegler said. </P>

