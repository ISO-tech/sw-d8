<P><font face="Times New Roman, Times, serif" size="4">Exhibit details cases of wrongful convictions</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Innocent and left to rot in prison</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Elizabeth Wrigley-Field</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 15, 2002 | Page 09</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>EXHIBIT: <I>Innocent: Inside Wrongful Conviction Cases,</I> </B>an exhibit at the John Jay College of Criminal Justice, 899 10th Ave., New York City, through November 30.</P></font>
<font face="Times New Roman, Times, serif" size="3"><I><P>INNOCENT: INSIDE Wrongful Conviction Cases</I> is a free exhibit based on research by Scott Christianson for a forthcoming book. Coming on the heels of national attention on wrongful convictions that sent innocent people to death row--and that pressured governors in Illinois and Maryland to declare a moratorium on executions--the exhibit focuses on showing that the problem is widespread in New York, too.</P>
<P>Wrongful convictions, says Christianson, are the product of "deep-seated and sometimes pervasive problems" in the criminal justice system. The exhibit's strong point is its many case studies that bring to life the nightmare of false convictions.</P>
<P>One example is Kelly Jarrett, a 48-year-old lesbian and the longest-serving woman in the New York state prison system. Jarrett was convicted of murder and sentenced to 25 years to life in 1975--after she was given a lawyer with no murder trial experience. </P>
<P>The main evidence against Jarrett was an "eyewitness" who--two years after the fact--picked her out as being "possibly the accomplice," based on a photo spread in which Jarrett's was the only picture with a "Sheriff's Department" label. Despite a 1982 affidavit from her codefendant that he had committed the crime and that Jarrett was not involved, she remains behind bars.</P>
<P>The exhibit does a particularly good job of showing how police and prosecutorial corruption can put people behind bars--and keep them there even after the corruption comes to light. One of the worst patterns of police corruption was uncovered by a study of the New York Police Department in Harlem from 1986 to 1994. According to the study, the exhibit says, "No fewer than one-sixth of cops in the 30th Precinct routinely robbed drug dealers, fabricated evidence, committed perjury and engaged in other crimes in the course of their explicit duties&#133;Even prosecutors said that false police testimony had tainted the evidence in at least 2,000 criminal trials." </P>
<P>In the case of Fernando Bermudez, information that could have cleared him was removed from a videotape of a police identification before it was given to the defense lawyer--a clear violation of the law. Although the doctoring of the tape has come to light, Bermudez is still serving a sentence of 23 years to life.</P>
<P>The exhibit's biggest failing is that it doesn't explicitly take up race or class as the main factors in determining who is convicted. But it does succeed in showing how New York's criminal justice system puts innocent people in prison and keeps them there. </P>
<P>And Christianson isn't interested in merely describing the problem. Since May 2001, when he selected 10 active cases to include in the exhibit, three convictions have been overturned. And Christianson says, "I won't be satisfied until justice is done in the other seven."</P>

