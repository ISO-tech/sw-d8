<P><font face="Times New Roman, Times, serif" size="5"><b>Bush gang takes aim at women's rights</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 15, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH and his gang are trashing women's rights again. The administration has threatened to withdraw support from the United Nations Population Fund (UNFPA), an organization that promotes family planning and women's reproductive health care internationally, and works to improve the legal and economic status of women.</P>
<P>At a conference in Bangkok earlier this month, the U.S. delegation announced that Washington wouldn't continue its support for the "program of action" adopted by the UNFPA at its population conference in Cairo, Egypt, in 1994--unless two key phrases were changed.</P>
<P>What phrases? "Reproductive health services" and "reproductive rights." According to the Bush White House, those phrases imply that a woman has the right to an abortion--and that's not acceptable to the right-wing hacks that run the show in Washington.</P>
<P>In July, the Bush administration withheld $34 million in aid earmarked for the UNFPA, claiming that there was evidence that the agency helps the Chinese government force women to have abortions--despite the fact that a State Department delegation to China found no credibility to the accusations.</P>
<P>More recently, the State Department froze $3 million in aid earmarked for the World Health Organization--because its Human Reproduction Program had conducted research on the abortion drug RU-486.</P>

