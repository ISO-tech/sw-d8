<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISRAEL</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Sharon calls new elections after Labor Party pulls out of government</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Will Israel lurch to the right?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 15, 2002 | Page 5</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>ERIC RUDER looks at the fight inside the Israeli government.</P>
</B><P>ISRAEL'S LABOR Party pulled out of the government of Prime Minister Ariel Sharon in late October, forcing him to call early elections for January.</P>
<P>The "national unity" government made up of Labor and Sharon's right-wing Likud Party collapsed during a debate over the government's 2003 budget, which allocated $147 million for Jewish settlers in the Occupied Territories. The $57 billion budget also contains $1.8 billion in cuts designed to push Israel's rapidly growing poor population into low-paying jobs formerly held by Palestinians.</P>
<P>The Labor Party bolted from the government in order to appeal to poor and working-class voters--in the desperate hope of winning votes away from Likud, which could nearly double its seats in the elections, according to polls.</P>
<P>Labor is following a carefully calculated strategy, playing to the racist resentment that Israelis shouldn't do "Arab work"--as unskilled jobs are called in Israel--while disavowing the settlers that many Israelis have come to regard as "too extreme."</P>
<P>But don't hold your breath waiting for the Labor Party to challenge Israel's massive military spending--$8.5 billion during the past 10 months alone--to maintain its grip on the West Bank and Gaza. Though some Israelis may criticize the settlers, simultaneously, there are growing calls for the forced expulsion of Palestinians from the Occupied Territories.</P>
<P>Thus, Sharon faces a stiff challenge later this month in a vote for leader of the Likud Party--from Benjamin Netanyahu, the former prime minister who favors "exiling" Arafat. Netanyahu claims that Sharon--a war criminal of the first order--is "too soft" on the Palestinians. In reality, Sharon's government has carried out massacres in Palestinian towns and placed most of the West Bank under indefinite military curfew.</P>
<P>For its part, the Labor Party--which presents itself as the party of peace--not only gave Sharon's government a fig leaf to carry out these atrocities, but it helped to orchestrate them. Benjamin Ben-Eliezer--the current head of the Labor Party--served as Sharon's defense minister until last week. Under his command, the Israeli military has killed hundreds of Palestinians as part of the campaign to smash all resistance to Israel's occupation.</P>
<P>"The culpability goes from the soldier who shot somebody with no necessity to those in the line of the command who ordered or condoned or covered up and to the highest authority of the state because they are politically responsible," said Amnesty International's Javier Zuniga in a recent report on the conflict.</P>
<P>For its part, the U.S. is concerned that a rightward-lurching Israeli government could provoke a crisis by exiling or killing Arafat--and derail the U.S. war drive against Iraq by setting off an explosion in the Middle East.</P>
<P>That's why U.S. Middle East envoy William Burns is peddling a "road map" designed to resurrect the "peace process" by proposing a way to achieve a Palestinian state in 2005. "But there were widespread suspicions that Burns was trying to sell snake oil," wrote Jonathan Cook in the <I>Al-Ahram Weekly</I>. "The road map looks less like an aid to navigating the long and tortuous route to a future Palestinian state and more like an American shortcut to its planned war with Baghdad."</P>
<P>In any case, the "road map" has all the same characteristics as previous U.S.-sponsored "peace" plans--promises about the future as Israeli settlements continue to expand and continued U.S. funding of Israel to the tune of $5 billion a year as Palestinian society is dismantled. U.S. support is critical to both Likud's platform of teeth-bared ethnic cleansing and Labor's kinder, gentler colonialism.</P>
<P>There won't be peace until there's a secular, democratic state in all of Palestine--where Jews and Palestinians enjoy equal rights.</P>

