<P><font face="Times New Roman, Times, serif" size="4">Safeway wants givebacks from Chicago grocery workers</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Dominick's attacks UFCW</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 15, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--"At some point, you have to stand up for yourself." That's what Edgar Pacheco, a shop steward with United Food and Commercial Workers (UFCW) Local 881 told <I>Socialist Worker</I>, as thousands of Chicago-area Dominick's supermarket workers voted to authorize a strike November 10.</P>
<P>Safeway Inc., which took over all 114 Dominick's stores in the Chicago area three years ago, is threatening to close them all down unless the 8,900 workers of Locals 881 and 1546 accept a concessionary contract.</P>
<P>Under management's proposed four-year deal, the company would cut health care coverage altogether for some workers, pay less than half the cost of coverage for new employees while also cutting starting pay, and would shift the majority of benefit cost increases onto current employees.</P>
<P>Management also wants to eliminate contract language that allows higher-seniority, higher-paid workers first access to available work hours, which could cause longtime workers to lose their health benefits completely.</P>
<P>All this would create a "two-tier" system for union employees at Dominick's--where newer employees receive significantly lower pay and fewer benefits, and where good jobs with more hours are effectively phased out.</P>
<P>Safeway claims that it has to cut costs--in order to compete with nonunion competitors like Wal-Mart that pay workers even less. But Dominick's workers aren't buying the rhetoric. "To me it's more than just this contract," said Local 881 shop steward Vicki Sneider, who's worked for Dominick's since 1973. "We've already got Wal-Mart. Are you going to let everybody do it? What's going to happen? Is everybody going to have to work two jobs?"</P>
<P>In fact, Safeway is the most profitable supermarket company in the country--and has seen profits rise 61 percent in the past two years, from $1.6 to $2.5 billion. The company has also been trying to pit Dominick's workers against each other--telling warehouse workers and meat cutters who are covered under different contracts that they will lose their jobs if Local 881 and 1546 workers strike.</P>
<P>But other union workers know that their jobs are also on the line. "We work at the warehouse, and our contract is coming up in 2004," said Steve Maglaya, a member of Teamsters Local 703, who came out to show solidarity during the strike vote. "Who knows what management going to ask for from us? We want to let [UFCW workers] know that if they go out, we're 100 percent behind them. We won't cross their picket lines&#133;and we'll be on any picket lines that go up."</P>
<P>In the run-up to the strike authorization, management held mandatory meetings--threatening workers with the loss of their jobs in the event of a strike. Safeway was so confident that it had scared the workers into voting against the strike authorization that it actually chartered dozens of buses to bring employees to union meetings for the vote.</P>
<P>They must have been shocked when 80 percent of the workers voted to authorize a strike. As <I>Socialist Worker</I> went to press, negotiations continued beyond the contract deadline as Safeway claimed that union leaders had "tampered" with a vote.</P>
<P>"We're not being unfair, we just want a good contract," said union activist Pacheco. "We don't want to lose our benefits and we don't want to lose our health and welfare. We want a good raise. We gave so much to this company. Why are they treating us like dogs?"</P>

