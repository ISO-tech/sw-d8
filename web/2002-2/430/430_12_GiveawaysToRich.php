<P><font face="Times New Roman, Times, serif" size="4">Republicans set to reward their corporate pals</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>No more giveaways to the rich!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 15, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHRISTMAS CAME early for Corporate America. On November 5, to be exact.</P>
<P>CEOs and corporate lobbyists are ecstatic about the Republicans' election victory, which puts the "party of big business" in charge of both houses of Congress--and all three branches of government. "This historic election sets the stage for aggressive action" on Corporate America's agenda, said Jerry Jasinowksi, president of the National Association of Manufacturers.</P>
<I><P>Business Week</I> declared that "Big and small business alike scored big in this election&#133;The Republican sweep of Capitol Hill will help Corporate America win some of its long-cherished goals: permanent tax cuts, new investment incentives, terrorism-insurance legislation, bankruptcy reform, and more. It'll also mean more pro-business energy policy and less regulation."</P>
<P>And while White House spin doctors ordered administration officials not to gloat over the Republican victory, employers didn't bother to conceal their glee. "Anybody that's looking for less regulation is happy," declared Scott Cleland, a lobbyist for the telecommunications industry in Washington. Sen. Trent Lott (R-Miss.), who will resume his post as Senate majority leader, declared, "I'm excited to be able to be on offense."</P>
<P>What does it mean for Lott to "be on offense"? More tax breaks for the rich, oil drilling in the Alaska National Wildlife Refuge and limits on lawsuits against corporations for a start. The Republican-led Senate will try to confirm a posse of conservative federal judges to push through a right-wing agenda to roll back civil rights and a woman's right to abortion. Then there's the drive to get rid of civil service and union protections in the new Department of Homeland Security--which Bush says is at the top of his agenda.</P>
<P>And of course, the Republican Congress will back Bush to the hilt on war--in Iraq and wherever else the White House wants to use the Pentagon to impose U.S. power.</P>
<P>The fact that the Republican agenda is--to judge from opinion polls--largely the opposite of what ordinary people want won't matter. The Republicans fudged their most unpopular proposals in order to hold onto votes on Election Day. And shamefully, the Democrats let them get away with it.</P>
<P>When the administration began to whip up a war drive frenzy about Iraq, the Democrats rolled over and gave Bush the congressional backing that he demanded--even as an antiwar movement was taking shape.</P>
<P>And despite a stock market meltdown and rising unemployment, the Democrats couldn't even make the economy a cutting-edge issue. That's because they refused to call for a rollback of Bush's tax cuts for the rich--even though the wealthiest 1 percent of the population will get 40 percent of the benefits.</P>
<P>On the question of prescription drug benefits for Medicare, "Senate Democrats folded, scrambling to embrace Republican proposals that they previously had rightly denounced as an insult," Robert Borosage of the Campaign for America's Future, wrote last summer.</P>
<P>In the months ahead, the Democrats may shift to the left--and claim to be fighting for working people again. But promises aren't enough. And even if the Democrats were serious, waiting for two years to stop Bush and his big business backers simply isn't an option.</P>
<P>Corporate America thinks it can get away with anything now. We need to build on every opportunity to organize the resistance--from picket lines and protests at the local level, to building the antiwar movement across the U.S. Because the only way to defend our interests is to organize and fight back--right now.</P>

