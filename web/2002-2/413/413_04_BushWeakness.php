<P><font face="Times New Roman, Times, serif" size="5"><b>The crack in the Bush facade</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 5, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>Matt Moreles criticized <I>Socialist Worker</I>'s coverage of the Bush administration's cover-up of pre-knowledge of the September 11 attacks as a "conspiracy theory" approach to politics (<I>SW,</I> June 21).</P>
<P>I agree with Moreles that conspiracy theories can make people feel that "the U.S. government is omnipotent" and that "resistance is impossible." But in this case, the much-asked question of whether Bush was or wasn't tipped off about the attacks is the first crack in the Bush fa&ccedil;ade after 9/11.</P>
<P>The scandal has given more people confidence to question the aims of this administration. It also exposes Bush's blatant disregard for the safety of the very American people in whose name he is killing thousands around the globe. </P>
<P>Moreles is correct to be concerned that this scandal can perhaps help the right wing with calls for a more centralized and brutal state machine. But for those trying to build an antiwar resistance, it is an opportunity to expose a Washington power structure whose aim has everything to do with politically and economically controlling the planet--and nothing to do with "protecting" us at home.</P>
<B><P>David Zirin, </B>Washington, D.C.</P>

