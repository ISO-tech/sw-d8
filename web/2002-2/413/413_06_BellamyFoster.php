<P><font face="Times New Roman, Times, serif" size="5"><b>"Capitalism can't solve these problems"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 5, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>JOHN BELLAMY FOSTER is co-editor of <I>Monthly Review</I> and author of <I>The Vulnerable Planet,</I> <I>Marx's Ecology</I> and <I>Ecology against Capitalism</I>. He spoke to <I>Socialist Worker</I> at the Socialism 2002 conference in June.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<B><P>HOW DO you answer people who say that Marxists are only concerned with labor, production and economics, rather than the environment?</P>
</B><P>HISTORICALLY, THAT wasn't true of Marx. He had a great deal to say about the environment, about pollution, about the destruction of the soil, about deforestation and climate change. He even talked about the commodification of species.</P>
<P>He had a huge concern about the issue of sustainability, separation of people from the land and so on. If you look over the history of it, you'll find that socialists had a bigger role in the development of ecological thought than just about anybody else.</P>
<B><P>WHY IS capitalism so destructive to the environment?</P>
</B><P>I THINK that one reason capitalism is so destructive is just the force of accumulation and growth and the fact that it's such an expansive system economically.</P>
<P>The way it expands is by increasing the throughput from resources into the economy--and then out, in the form of waste, into the environment. The throughput keeps on increasing along with the economy.</P>
<P>It's so expansive that if you have a 3 percent rate of economic growth, that means that in a century, the economy will increase to 16 times its present size. And in two centuries to 250 times its present size, and in three centuries to 4,000 times its present size. You're dealing with an environment that is, in some ways, limited. So it's this massive growth within a limited form.</P>
<P>Of course, we all believe in economic growth to some extent. But under capitalism, it's also growth without the right priorities. It leaves people behind in poverty, rather than advancing everyone. It involves the production of all sorts of wasteful and toxic products so that the level of toxicity keeps going up because they're incorporating more and more chemicals and relying on nuclear power and so on.</P>
<P>But beyond that, there's a problem because capitalism involves the alienation of nature. It involves the separation of human beings from nature. Capitalism creates an antagonistic relationship between town and country and between economy and nature. And it divides nature in extreme ways, just as it divides labor among human beings. It's what Marx called a "metabolic rift."</P>
<P>It's a very unsustainable form of development. We have to get beyond this alienation of nature in order to be able to solve the problem. And capitalism can't do it.</P>
<B><P>WHAT WILL it take to save the environment?</P>
</B><P>RADICAL ENVIRONMENTALISTS recognize that there has to be quite radical change in society. But they generally haven't understood that this means moving away from capitalism or overthrowing capitalism in some way, either by a short revolution or a long revolution--but a revolution against capitalism.</P>
<P>They haven't understood the extent to which ecological contradictions are built into capitalism and the fact that capitalism can't get very far in dealing with ecological problems. It can only do quite minor things.</P>
<P>If you look at global warming, the scientists are saying that the emissions have to be reduced by 70 to 90 percent from 1990 levels. The Kyoto Protocol was only going to reduce emissions by 5 percent. And the United States wasn't even able to do that, because they figured that they'd have to reduce their emissions by 2010 by 30 percent--just to get down to the 5 percent below 1990 levels.</P>
<P>There's just no way that this system can get there. And the Japanese aren't able to reduce by that amount, and the Europeans aren't. So now, the Bush administration is saying that it's too late to do anything about it. It's admitting that capitalism can't deal with global warming.</P>
<P>And so we're just supposed to "adapt" to the heating up of the earth. I think for most people in the world, that's not acceptable.</P>
