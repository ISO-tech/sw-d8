<P><font face="Times New Roman, Times, serif" size="5"><b>"She never gave up hope for the future"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 5, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>, </P>
</B><P>June Jordan, the African American poet, activist and teacher, died from breast cancer on June 14 at the age of 65. </P>
<P>An award-winning author of poetry, political essays and fiction, and a longtime columnist for <I>The Progressive</I> magazine, Jordan was one of the most prolific African American writers in history. She was also a beloved professor of African-American Studies at the University of California-Berkeley, where she founded "Poetry for the People," a program training undergraduates to teach and encourage political activism and artistic awareness among children in public schools. </P>
<P>As a teacher, she focused her attention on the women poets of the Harlem Renaissance, among others, critiquing the kinds of feminism that overlook class, race and children in women's lives.</P>
<P>She became a politically active poet when living in a public housing project in New York City and working with the Congress of Racial Equality (CORE) in the 1960s. "I remember hearing Dr. King on the radio saying, 'If any blood shall flow in the streets of Birmingham, let it be our blood and not the blood of our white brothers and sisters,'" she said. "I really thought he had lost his mind&#133;I really thought, 'No way. It's not gonna be our blood.' From that point forward, I decided I was 'in,' but not nonviolent,'" she said.</P>
<P>Most recently involved as a poet and activist in the ongoing struggle of the Palestinian people for national liberation, Jordan's passion and courage inspired all who knew her and read her work. </P>
<P>As Jordan's fellow professor at Berkeley, Carolyn Porter, recalled, "She never gave up hope--not only in regard to her illness, but also for the world and its possible future. And she had good reason for hope, since she changed countless--and I do mean countless--lives."</P>
<B><P>Deborah Roberts,</B> Chicago</P>

