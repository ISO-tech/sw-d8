<font face="Times New Roman, Times, serif" size="5"><b>Protesters' deaths shake Argentina</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Todd Chretien</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 5, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE KILLING of two demonstrators by police who fired indiscriminately into a protest by unemployed workers last week is sending political shock waves through Argentina.</P>
<P>The murders took place in an industrial suburb of Buenos Aires, where activists of the militant unemployed movement--known as the <I>piqueteros,</I> or picketers--attempted to block a bridge into the capital.</P>
<P>Police fired several volleys into a demonstration of about 1,000 <I>piqueteros,</I> injuring close to 100 people. The cops then chased a small group of activists into a train station--and gunned down two in cold blood as a photographer stood by. The next day, eight photos documenting the murders, moment by moment, headed the country's main daily newspaper <I>El Clar&iacute;n</I>.</P>
<P>Within hours, a protest made up of 40,000 <I>piqueteros, </I>unionists, students, activists from neighborhood organizations and left-wing political parties defiantly took over the Plaza de Maya in downtown Buenos Aires--the site of a police massacre last December during the uprising that toppled two presidents in a period of a week.</P>
<P>The police repression--and the massive response against it --mark an important development in Argentina. President Eduardo Duhalde, who took over the presidency at the end of December after weeks of mass demonstrations forced out his predecessors, is under increasing pressure to call early elections.</P>
<P>He is trying to ram through ever more drastic austerity programs, which have already driven half the population under the poverty line. In a desperate attempt to divide the most militant activists from more moderate sections of the movement, Duhalde and his police began threatening to crack down on "violent" protesters--while respecting "law-abiding" dissent. </P>
<P>By attacking the <I>piqueteros </I>as they blocked a bridge, Duhalde hoped to vilify them and set the stage for a wider attack on the far left of the movement. But his plan backfired. Mass anger at the police killings forced him to fire the Buenos Aires police chief, arrest the two cops who pulled the triggers and suspend more than 100 other police.</P>
<P>July 9 has been set as a national day of action against Duhalde's austerity and repression and is endorsed by the <I>piqueteros, </I>radical neighborhood associations, most far-left parties and the Argentine Workers' Center, the most left-wing of the country's union federations.</P>
<P>It will be the latest, though certainly not the last, in a series of confrontations over the coming months.</P>

