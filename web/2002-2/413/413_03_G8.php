<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>A slap in the face to the world's poor</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 5, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>LARGE SUMS of money have been in the news lately--such as the $3.9 billion accounting fraud by WorldCom. Except in the stories about humanitarian aid for the world's poorest countries.</P>
<P>When the leaders of the Group of Eight (G-8) club of the world's most powerful countries gathered last week for a summit at a secluded resort town in Canada, there was a lot of talk about lifting African nations out of poverty. But no action. The summiteers came up with a miserly $1 billion in possible debt relief for a continent that is strangling on its debt burden.</P>
<P>The G-8 spent that much on organizing its last five summits. Phil Twyford of the British relief group Oxfam called the amount peanuts--"and repackaged peanuts at that."</P>
<P>Naturally, the U.S. tried to get off as cheaply as possible. Even <I>Wall Street Journal</I> columnist Alan Murray had to admit that the U.S.'s "micro initiative" of $20 million more a year for "education" in Africa was cheap. "It's hard for the world to see the big hearts of U.S. leaders when they must peer through such a small wallet," he wrote.</P>
<P>The U.S. spends less than 0.1 percent of its gross national product on foreign aid, the lowest of the developed world. And in return for this insult, George W. Bush has big demands.</P>
<P>In Canada, Dubya followed up on the "compassionate conservative" theme that he pushed at the United Nations conference on poverty in Mexico last March. Thus, under the New Partnership for Africa's Development program, countries might get a piece of the $6 billion in aid provided by the G-8 governments starting in 2006--but only if they implement reforms first.</P>
<P>Demanding that poor nations open up their markets and compete is pure hypocrisy coming from the U.S. Just a month ago, Bush signed a $51 billion increase in government farm subsidies--which will almost all end up in the pockets of wealthy agribusinesses.</P>
<P>If the G-8 were committed to ending world poverty, canceling the debt imposed by world financial institutions like the International Monetary Fund and World Bank would be the first step. But that's not likely, with the U.S. calling the shots.</P>
<P>It's clear that changing world leaders' warped priorities won't come without a struggle.</P>

