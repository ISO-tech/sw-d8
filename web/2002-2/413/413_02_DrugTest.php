<P><font face="Times New Roman, Times, serif" size="5"><b>Drug tests for the chess team</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 5, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE Supreme Court last week okayed random drug tests for any student who participates in "competitive" extracurricular activities.</P>
<P>The justices decided that it's legal for an Oklahoma school district to require drug testing for students to be eligible for a range of programs. Not only will high school athletes be subject to the ruling, but so will chess team members, cheerleaders and members of choir. Anyone who refuses testing will be barred from participating in extracurricular activities.</P>
<P>"The court's decision is both foolhardy and dangerous in sending exactly the wrong message to America's schoolchildren--that they have no right to privacy and that schools can prioritize the cleanliness of urine over academic achievement and participation in student life," said Judy Appel, deputy legal director for the Drug Policy Alliance.</P>

