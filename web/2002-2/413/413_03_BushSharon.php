<font face="Times New Roman, Times, serif" size="4">The Bush administration lines up behind Sharon's terror</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>It's right to resist Israel's apartheid</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 5, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE PALESTINIAN cities of the West Bank aren't so much cities today as large open-air prisons.</P>
<P>For the last week, the curfew imposed by Israeli troops in the West Bank has been unrelenting. As many as 750,000 people are under effective house arrest. The streets are deserted. Stores, schools--everything is closed. In Qalqilya, three children were injured when Israeli soldiers opened fire at a crowd of students who thought that the curfew had been lifted--so that they could go to school.</P>
<P>This is terrorism at its most brutal and systematic. But you'll never hear it described that way by the U.S. media. Instead, the press was filled last week with solemn "analysis" of George W. Bush's despicable June 24 speech, in which he so shamelessly tailed the rhetoric of Israeli Prime Minister Ariel Sharon that a few commentators suggested Sharon's press office had written it.</P>
<P>Bush's demand that Palestinians dump Yasser Arafat before the U.S. will consider supporting a "transitional" Palestinian state--whatever that is--was Sharon's idea. After Israel's murderous invasion of the West Bank in the spring, Sharon began concocting a case against the Palestinian Authority as a stalling tactic to cover for Israel's continued occupation and annexation of Palestinian land. Now Bush has signed on without condition. </P>
<P>To believe that Palestinians--already enduring Israel's military occupation and economic strangulation--could meet Bush's list of impossible conditions is sheer fantasy. But it's a useful fantasy for Bush and Sharon --because it helps to cover up the true roots of the Middle East conflict.</P>
<P>The truth is that Israel was established on stolen Palestinian land in 1948, when Zionist forces expelled nearly 1 million Palestinians. The Palestinian demand for self-determination--for their own state, for the right of refugees to return to their homes, and for the right to choose their own representatives--is historically just.</P>
<P>But Israel's existence depends on denying Palestinian self-determination. That's why the U.S. government has always aided and abetted Israel's crimes against Palestinians--to prop up its main ally in the oil-rich Middle East.</P>
<P>Now, Bush has essentially said that he won't accept the legitimacy of Palestinian elections unless they produce the result that Washington wants. It's as if, in 1776, Britain announced that it might allow the 13 colonies to become independent--as long as Americans chose a pro-British military dictator to rule them.</P>
<P>Where does Bush get off dictating to Palestinians who their leaders should be? Can you imagine the outrage if the tables were turned?</P>
<P>Of course, U.S. presidents have ordered the overthrow of many governments before. The only difference now is Bush's brazenness in calling for a "regime change"--in Palestine, in Iraq and anywhere else he wants to.</P>
<P>Self-determination for the oppressed isn't a reward for good behavior. It's a fundamental democratic right. The Palestinians' demand for self-determination is wholly legitimate, and anyone who cares about justice should support their resistance to Israel's apartheid.</P>

