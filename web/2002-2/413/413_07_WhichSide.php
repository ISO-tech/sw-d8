<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Behind Afghanistan's new "democracy"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 5, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THROUGHOUT Afghanistan's loya jirga, or grand tribal council, held last month, the U.S. media focused attention on the raucous debates between delegates and the smattering of women who took to the microphones to demand women's rights. The loya jirga, it seemed, represented a showering of democracy made possible by the U.S. bombs that drove out the Taliban--a validation of the first phase of the war on terrorism.</P>
<P>But closer examination (a practice the U.S. media avoids at all costs) reveals that the loya jirga was merely a public relations exercise on behalf of an illegitimate government and the illegitimate war that put it in power.</P>
<P>Most Americans would be surprised to learn, for example, that interim leader Hamid Karzai announced his own election as president before the vote had actually taken place, to the dismay of many delegates. Karzai apparently mistook the decision-making behind the scenes with the public relations stunt on the assembly floor.</P>
<P>Minister of Women's Affairs Sima Samar--ceremoniously introduced by George W. Bush during January's State of the Union Address as a symbol of U.S. commitment to women's rights in Afghanistan--was unceremoniously dumped from her post after fundamentalists circulated a petition denouncing her as "Afghanistan's Salman Rushdie." "This is a rubber stamp," said Samar. "Everything has already been decided by the powerful ones."</P>
<P>Even calling the loya jirga a valid election is a stretch. As Gary Leupp wrote in <I>CounterPunch,</I> "The warlords currently enjoying U.S. support largely determined the selection of delegates." Those warlords are the warring thugs from the Northern Alliance, who have continued to consolidate their power through rape and murder since the fall of the Taliban, while the U.S. looks the other way.</P>
<P>Eight delegates elected to the loya jirga were murdered in May amid a general rise in political violence and intimidation by warlords guarding their own fiefdoms. "When election observers entered the city of Gardez, the local commander fired rockets at them," noted one United Nations observer.</P>
<P>Karzai used a rumored plot to overthrow his government--as yet unconfirmed--as an excuse to round up 700 of his political opponents in the weeks before the loya jirga. And throughout the loya jirga proceedings, U.S. State Department envoy Zalmay Khalilzad--a former Unocal oil executive--worked hand in glove with Karzai and the Northern Alliance to manipulate the votes behind the scenes.</P>
<P>When a groundswell of support emerged to elect former king Zahir Shah as head of state, Khalilzad delayed the start of the proceedings for nearly two days while the king was strong-armed into withdrawing his name from consideration.</P>
<P>Interim Defense Minister Mohammed Fahim threatened to withdraw all Northern Alliance delegates unless the former king backed out. Fahim--who placed his troops on alert to underscore the seriousness of this threat--was later named defense minister and deputy president.</P>
<P>Through blackmail, bribery and military force, the U.S. has determined the political landscape of post-Taliban Afghanistan.  Most of the $4.5 billion of aid promised to Afghanistan in January by rich donors, including the U.S., was held back pending the desired outcome at the loya jirga. And since last winter, U.S. troops have roamed the countryside in the company of various warlord gangs, whose loyalty it bought with money and weapons.</P>
<P>The CIA made payments as high as $100,000 or more to individual warlords, in a country where the average monthly income is just $1.20. "Afghanistan's warlords are stronger today than they were 10 days ago before the loya jirga started," argued Vikram Parekh of Human Rights Watch on June 20. </P>
<P>An expansion of peacekeeping troops under U.S. control beyond Kabul, as many have proposed, would not loosen the hold of the warlords. Their re-emergence did not come about despite U.S. intervention but because of it.</P>
<P>As Leupp argues, "Global oppressors can't build nations that deliver justice to their citizens"--logic worth keeping in mind as the U.S. plans its next "regime change," in the next phase of its war on terrorism, against Iraq.</P>

