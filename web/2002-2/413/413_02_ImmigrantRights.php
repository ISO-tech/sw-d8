<P><font face="Times New Roman, Times, serif" size="5"><b>A tragic accident exposes U.S. policy</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Chuck Stemke</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 5, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>A TERRIBLE tragedy on a highway near San Diego last week cast a spotlight on the cruelty of the U.S. government's crackdown on illegal immigrants.</P>
<P>Six people were killed and 31 injured in a high-speed collision involving a van packed with undocumented workers. The van, with 33 immigrants plus a driver inside, had crossed the rugged mountains along the California-Mexico border and was headed toward Los Angeles and Las Vegas when the Border Patrol began to chase it. In a maneuver to escape his pursuers, the van driver crossed into oncoming traffic with his headlights off, crashing into another car.</P>
<P>Incredibly, the Border Patrol claims that the accident is evidence of the need for tougher enforcement. But the government's ongoing militarization of the border has made horrible tragedies more likely.</P>
<P>Immigrants have been forced to take even greater risks to get to the U.S. Since the Clinton administration launched Operation Gatekeeper in 1994 with the construction of a steel wall, installation of powerful floodlights and more weapons for Border Patrol agents, at least 700 people have died trying to cross over in the desert and rugged mountains further east of San Diego.</P>
<P>Meanwhile, the smugglers, or coyotes, are often dangerous criminals who exploit and further victimize their clients. And undocumented workers who survive the crossing have nothing better to look forward to than a miserable low-wage job.</P>
<P>We don't need a restrictive border that makes tragedies like last week's inevitable. We need to demand that the U.S. government open the border to workers--and fight for equal rights for immigrants here in the U.S.</P>

