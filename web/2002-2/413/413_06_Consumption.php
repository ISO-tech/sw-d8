<P><font face="Times New Roman, Times, serif" size="5"><b>Are we consuming too much?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 5, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ARE WORKING people to blame for the environmental crisis? This is a common idea put forward by environmentalists--that everyone in the industrialized world is guilty of wrecking the environment because we lead wasteful lives and consume too much.</P>
<P>For example, Dinyar Godrej, author of <I>The No Nonsense Guide to Climate Change,</I> argues that people in the West have a "'we consume because we can' mentality which is straining the environmental resources of the Earth to its limits and has created chasms of inequality between nations."</P>
<P>But by focusing on individual behavior rather than the way that the system is organized by our rulers in the interests of profit, Godrej, however well-meaning, is blaming the wrong people--and offering no real challenge to the system.</P>
<P>It is true that, with only 4 percent of the world's population, U.S. per capita consumption is 14 times greater than in the lowest-income countries with 40 percent of the world's population. But that doesn't mean that everyone in the U.S. consumes equally. </P>
<P>The richest 1 percent of families own almost 40 percent of all the country's wealth. The idea that we should all "sacrifice a little" is a slap in the face to the millions of people in this country who struggle each day to get by.</P>
<P>Take the example of cars. "Journalists never tire of pointing to the love of the automobile in the United States," writes John Bellamy Foster, co-editor of <I>Monthly Review</I>. "But such 'love' is more often than not a kind of desperation in the face of extremely narrow options. The ways in which cars, roads, public transport systems (often notable by their absence), urban centers, suburbs and malls have been constructed mean that people have virtually no choice but to drive if they are to work and live."</P>
<P>In fact, for most working people in the U.S., cars aren't a luxury but a necessity imposed on them by the way the system is organized.</P>
<P>Arguments in favor of reducing consumption also assume that if Americans, for example, ate less, the food would get to the 800 million people in developing countries who go hungry each year. Not only does that not happen, but internationally, there's no shortage of food. "The world today produces more food per inhabitant than ever before," says the Institute for Food Development and Policy. "The real causes of hunger are poverty, inequality and lack of access."</P>
<P>Targeting consumers lets the real culprits--the corporations--off the hook. Indeed, for every one ton of waste produced through consumption, five tons are created in the production process, and a whopping 20 tons are produced through the initial extraction of resources.</P>
<P>Arguments against individual overconsumption don't address this. Instead, they accept the idea that people can make a change in the priorities of society by "voting with their dollars"--that is, making a socially responsible choice among the very narrow options we have in what to eat, what to wear and where to shop.</P>
<P>Working people don't have nearly the power as consumers that they do at what Karl Marx called "the point of production." That's because workers make every product that lines supermarket shelves--and they build the shelves and the store, too.</P>
<P>A society run by the working majority could change the social priorities--and create a world that both meets people's needs and protects the planet we live on.</P>

