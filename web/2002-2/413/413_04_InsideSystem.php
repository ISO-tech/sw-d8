<font face="Times New Roman, Times, serif" size="5"><b>Insider trading--it's a good thing</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 5, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>IF MARTHA Stewart goes to jail, maybe they'll let her make curtains to cover the bars.</P>
<P>The queen of "ordered living" seems to have run afoul of the Securities and Exchange Commission these days. She's being investigated for a shady trade of shares of ImClone, a biotechnology company.</P>
<P>It seems that the day before the company received a negative ruling from the government on a drug application which sent share prices plummeting, Stewart sold 3,928 shares of stock--a whopping $227,000 worth.</P>
<P>Sam Waskal, Stewart's pal and ImClone's ex-CEO, has been charged with tipping off relatives about the rejection--maybe Stewart among them.</P>
<P>It's hard to tell if any of the "mess" is starting to trouble the more-perfect-than-thou Stewart. She still manages to grin while repeating her famous tagline, "It's a good thing," on her daily CBS show.</P>
<P>But recently, on CBS's <I>Early Show,</I> Stewart told host Jane Clayson, "I will be exonerated of any ridiculousness." When Clayson tried to press the issues, Stewart replied, "I want to focus on my salad"--and went back to chopping cabbage with a 10-inch knife.</P>
<P>--<I>New York Post,</I> June 26, 2002</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Hot dog of mass destruction</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. government has added a new vehicle to its list of terrorist weapons: the Oscar Mayer Wienermobile.</P>
<P>Last week, a pair of Oscar Mayer employees driving the Wienermobile--a hot dog-shaped truck--got pulled over by police for "driving too close to the Pentagon." The college students manning the controls had accidentally pulled onto Route 110, which has been closed to trucks ever since September 11.</P>
<P>"A police officer...was kind enough to direct traffic to allow the Wienermobile to exit," said Oscar Mayer spokewoman Sarah Delea. "They didn't see any signs, but now they know. They'll make sure not to go down Route 110 again."</P>
<P>"This hot dog posed no threat to us," said Virginia State Police spokesperson Lucy Caldwell.</P>
<P>--United Press International, June 21, 2002</P>

