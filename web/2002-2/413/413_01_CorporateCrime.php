<font face="Times New Roman, Times, serif" size="5"><b>Stop these corporate crooks!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 5, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>NOT LONG ago, Deborah Day figured that she had a secure job. The company where she worked as a computer technician was an up-and-coming telecommunications giant that bragged constantly about its limitless potential.</P>
<P>Now Day worries how she'll support her 7-year-old son as an unemployed single mother. That's because WorldCom executives got caught committing a massive bookkeeping fraud--to the tune of $3.9 billion--which plunged the company into a tailspin toward bankruptcy.</P>
<P>Day and 17,000 other WorldCom workers will pay the price--they got laid off last week. "When you think about all the assets this company had, it just makes you so angry," Day told a reporter as she choked back tears.</P>
<P>WorldCom is the latest on the growing list of corporate meltdowns. While the collapse of energy giant Enron late last year exposed an intricate web of partnerships to hide the company's growing debts, the deception at WorldCom was simple. Instead of reporting the cost of renting phone lines from other companies as an operating expense, former chief financial officer Scott Sullivan listed it as an investment that would produce future profits.</P>
<P>Any first-year accounting student could have spotted the scam. So why didn't WorldCom's auditor?</P>
<P>Because Arthur Andersen was the auditor--the same company that okayed the cooked books at Enron. And Dynegy. And Global Crossing. And Qwest. The claim that these scandals are the result of a few "bad apples" is wearing thin.</P>
<P>Throughout the 1990s, Wall Street's demand for superprofits led corporations of all kinds to pump up their bottom lines with accounting trickery. No one asked questions--not stockbrokers, not bankers, not auditors--until the recession took hold, and some of the biggest giants of Corporate America were exposed as con artists.</P>
<P>"Everybody did this," says economic historian Peter Temin of the Massachusetts Institute of Technology. "The people who got in trouble are those who are most out on the edge. Enron didn't get caught. Enron got so far out on the edge that it fell off."</P>
<P>Some of the biggest crooks may yet go to jail. But many of their worst crimes will never be punished. Because it's perfectly legal to wreck workers' lives in the drive for more profits.</P>
<P>We need to put an end to corporate crime once and for all--by fighting for a socialist alternative.</P>

