<P><font face="Arial, Helvetica, sans-serif" size="2"><b>A PEOPLE'S VACATION GUIDE</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">The site of John Brown's raid against slavery</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The fire of abolition at Harper's Ferry</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Lalasz</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 5, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>HARPER'S FERRY, W. Va., located where the Potomac and Shenandoah Rivers merge and the states of West Virginia, Maryland and Virginia meet, is a gorgeous place for hiking, biking, rafting and kayaking. But the best part of going there is learning about John Brown's raid in 1859, a pivotal event in the fight against slavery.</P>
<P>The National Park Service's John Brown Museum is surprisingly favorably to the raid, putting Brown's fight in the context of the horrors of slavery. The first room of the exhibit details how dehumanizing life was for slaves in the South--bought and sold as property, separated permanently from their families. It makes clear why Brown and others committed themselves so passionately to the fight to end slavery.</P>
<P>Brown believed that he could free the slaves and selected Harper's Ferry as his starting point. On October 16, 1859, Brown and 21 other raiders took over the U.S. Armory and Arsenal firehouse. The raiders--made of up slaves, freed slaves, free-born Blacks and white abolitionists--hoped to seize the weapons at the Arsenal to arm an uprising of slaves and use the Blue Ridge Mountains for guerrilla warfare.</P>
<P>Unfortunately, the raid was short-lived, as Brown and the raiders were severely outnumbered. They held the armory for 36 hours, but federal troops were sent in, and on October 18, took Brown and the others captive. Found guilty of treason--of conspiring with slaves to rebel and murder--Brown was tried, convicted and hanged on December 2, 1859.</P>
<P>But despite this tragic end, Brown remained a committed opponent of slavery, issuing a warning right before his death: "I wish to say furthermore, that you had better--all you people in the South--prepare yourselves for a settlement of that question that must come up for settlement sooner than you are prepared for it&#133;You may dispose of me very easily; I am nearly disposed of now; but this question is still to be settled--this Negro question I mean--the end of that is not yet."</P>
<P>Brown was right. Although Brown's raid failed, it became a turning point, focusing national attention on the fight against slavery. It was also the catalyst for the Civil War, which began two years later.</P>
<P>As Black abolitionist Frederick Douglass said in 1881: "The true question is, Did John Brown draw his sword against slavery and thereby lose his life in vain? And to this I answer ten thousand times, No! No man fails&#133;who so grandly gives himself and all he has to a righteous cause."</P>
<P>Because of the incredible impact Brown's raid on the fight against slavery, Harper's Ferry became a place where Blacks came to live. Storer College, established in 1867, was an integrated school designed primarily to educate former slaves. Douglass was on its board of trustees.</P>
<P>This site is well worth visiting. Harper's Ferry is an inspirational vacation spot for anyone committed to fighting racism today.</P>
