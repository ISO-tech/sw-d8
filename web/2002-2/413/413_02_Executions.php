<P><font face="Times New Roman, Times, serif" size="5"><b>Judge declares federal executions unconstitutional</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 5, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>TOO MANY innocent people sit on death row. This fact led a New York judge to rule on July 1 that the federal death penalty system is unconstitutional.</P>
<P>U.S. District Court Judge Jed Rakoff explained his decision by citing "the unacceptably high rate at which innocent persons are convicted of capital crimes, when coupled with the frequently prolonged delays before such errors are detected."</P>
<P>Rakoff, a former federal prosecutor, based his ruling on research that shows evidence of a person's innocence often doesn't come out until long after their conviction--making it entirely possible for inmates to be executed before they can prove their innocence.</P>
<P>Responding to the argument of death penalty supporters that DNA testing will reduce the risk of mistaken convictions, Rakoff wrote, "This completely misses the point. What DNA testing has proved, beyond cavil, is the remarkable degree of fallibility in the basis fact-finding processes on which we rely in criminal cases."</P>
<P>The federal government is certain to appeal Rakoff's ruling--and find an appeals court to overturn it. But taken with the U.S. Supreme Court's rulings limiting capital punishment and the state of Maryland's recent moratorium on executions, Rakoff's decision is another sign of the growing public questioning of the death penalty system.</P>
<P>"Judges are starting to realize what many people in the American public realize&#133;that the system is irreparably flawed," said David Elliot from the National Coalition to Abolish the Death Penalty.</P>

