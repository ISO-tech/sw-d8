<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>One nation under flag-waving hypocrites</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 5, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>A FEDERAL appeals court ruling on the Pledge of Allegiance last week sent politicians of every stripe scuttling to sound off in front of the TV cameras.</P>
<P>The decision--which found that the phrase "under God" in the Pledge is unconstitutional--caused such a furious reaction that the three federal appeals court judges who made it blocked their own ruling. The California doctor who originally filed the suit got death threats from especially devout Christians.</P>
<P>Republicans tried to use the uproar to blame Senate Democrats for holding up George W. Bush's nominees for federal judgeships. Problem. Judge Alfred Goodwin, who wrote the opinion, was appointed by a Republican president, Richard Nixon.</P>
<P>But just to be sure they wouldn't be tagged as "atheists," Democrats denounced the decision every bit as loudly.</P>
<P>Given the unanimous outcry in Washington, it's no surprise if many ordinary people think that the ruling was, at the very least, strange. The grounds for dismissing the decision as "political correctness run amok" have been carefully prepared for years. But anyone who accepts Washington's line should take a closer look at what the politicians actually said.</P>
<P>George W. Bush, for example, blustered that the decision was "out of step with the history and traditions of America" and "points up the fact that we need common-sense judges who understand that our rights were derived from God."</P>
<P>So much for the separation of church and state--an idea that the American radicals who fought to win independence from British colonial rule insisted on in their new Constitution. With all its limitations, this document was a revolutionary political advance in a world where kings, emperors and tsars still claimed to rule by "divine right."</P>
<P>The words "under God" weren't even in the Pledge when it was written in 1892. They were added in 1954 at the height of the McCarthyite witch-hunts--as a proposal from the reactionary Knights of Columbus explicitly designed to further marginalize the "godless communists." No wonder Bush and friends are so attached to the phrase today.</P>
<P>As socialists, we have better reasons than the words "under God" for rejecting the Pledge of Allegiance. But we shouldn't miss the opportunity to point out how the politicians are whipping up this frenzy in order to wrap themselves in both the flag and God--and at the same time change the subject to political issues that matter more directly in working people's lives.</P>

