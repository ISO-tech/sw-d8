<P><font face="Times New Roman, Times, serif" size="4">U.S. warplanes bomb Afghan wedding</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The Pentagon's latest deadly mistake</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 5, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"THERE ARE no Taliban or al-Qaeda or Arabs here. These people were all civilians, women and children." That's what a resident of the village of Abdul Saboor in Afghanistan told BBC News after the U.S. military's latest--and deadliest--blunder.</P>
<P>In an overnight raid on July 1, U.S. warplanes bombed a wedding party in the central province of Uruzgan. By midday, the "official" estimate of casualties had fallen to 30, but reporters on the scene say that as many as 250 people were killed and 400 wounded.</P>
<P>Officials of the Afghan government of U.S. stooges admitted that U.S. warplanes mistook wedding participants firing into the sky for anti-aircraft fire and swooped in for an attack. But the Pentagon isn't admitting anything. "We are aware of reports of civilian casualties, but don't know if casualties were caused as a result of the bomb," sniffed Pentagon spokesperson Lt. Cmdr. Jeff Davis.</P>
<P>In fact, one survivor told reporters that the attack began at about 2 a.m. and continued until 4 a.m.--after which U.S. Special Forces soldiers invaded the area. "The Americans came and asked me, 'Who fired on the helicopters?' and I said, 'I don't know,'" he said. "One of the soldiers wanted to tie my hands, but someone said, 'He's an old man.'"</P>
<P>According to workers at the Mir Wais hospital in Kandahar, most of the dead were women and children. One injured 6-year-old girl was brought to the hospital still wearing her party dress. Every member of her family had been killed in the bombing. "The villagers brought these children, and they have no parents," nurse Mohammed Nadir told the BBC. "Everyone says that their parents are dead."</P>
<P>This brutal U.S. blunder should come as no surprise. It's simply the latest in a series of bombing mistakes that have killed thousands of innocent Afghan civilians since the beginning of the war.</P>

