<P><font face="Times New Roman, Times, serif" size="4">Bloomberg wants to balance the budget on workers' backs</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Support Queens bus drivers</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 5, 2002 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--About 2,000 Queens bus drivers and their supporters rallied in front of City Hall last week to press New York City Mayor Michael Bloomberg as their strike entered its third week.</P>
<P>The drivers from Transport Workers Union (TWU) Local 100 are striking for better health care and job security. But Bloomberg wants to make an example out of this strike--to show that he will take a hard line against city unions in order to push through budget cuts. </P>
<P>"In the 32 years I've been working here, this is the worse contract ever!" explained one driver. The city is losing more money per week than what the drivers are demanding for the next three years in their contract, but negotiations are still deadlocked. </P>
<P>Bloomberg claims that there's nothing he can do. "If the union wants better benefits, they are free to bargain with their employer," he sneered.</P>
<P>But the truth is that the city promised TWU last December that it would fund increased contributions to drivers' health care at the same rate enjoyed by municipal employees. Then the city broke its promise and now is offering increases of just 3.5 percent over two years compared to the 19.8 percent increase it is giving to municipal employees. </P>
<P>Bloomberg hopes that by bullying Queens bus drivers--whom he considers to be the weak link in TWU--he can go into December's Metropolitan Transit Authority (MTA) negotiations in a strong position.</P>
<P>And the contract for city workers in DC 37 also expired this week--another union that Bloomberg hopes will get his message.</P>
<P>Some strikers are beginning to worry about their own financial situations, so it's become critical to step up pressure on the city. Most of TWU's 30,000 members work directly for the city as part of the MTA that runs the city's subway trains and city buses. Queens bus drivers, on the other hand, are hired by private companies, which receive subsidies from the city.</P>
<P>MTA workers need to stand in solidarity with Queens bus drivers. TWU Local 100 is calling on its members not to take on voluntary overtime that would help the city run buses parallel to the routes that Queens bus drivers usually cover because this would constitute scabbing on the strike. And the union is asking its members to visit picket lines, circulate petitions and collect money for the strikers.</P>
<P>Despite the inconvenience that transit strikes cause, hundreds of thousands of people have been supportive of TWU workers. We need to keep making the case that a victory for the strikers will be a victory for all workers. This kind of campaign can turn the motto that "an injury to one is an injury to all" into reality. </P>
<P>Moreover, MTA workers face their own contract battle next December, so solidarity action now will help Queens bus drivers as well as build unity to prepare TWU Local 100 for its next contract battle with Bloomberg. </P>
<P>Solidarity with Queens bus drivers!</P>

