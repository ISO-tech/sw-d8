<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>How "natural" are natural disasters?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 5, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WE MARXISTS are sometimes rebuked for blaming everything on capitalism. Yet often, if a chain of events is traced back carefully, capitalism is to blame--as in the case of an accident caused by an overworked trucker, for example.</P>
<P>But surely there are terrible natural events that have nothing to do with capitalism. Certainly, if a giant meteor strikes the earth, no one in their right mind would blame it on capitalism.</P>
<P>Nevertheless, there are ways in which capitalism shapes the impact of naturally occurring events. Take, for example, the wildfires that are raging in the Western states and that already have exceeded the total acreage affected by fires last year.</P>
<P>The media often blame these fires on the forces of nature: hot, dry conditions and several years of drought. To the extent that human beings play any role, it's as individual scapegoats.</P>
<P>But while some of these fires may have begun as a result of the negligence of individuals, experts were already predicting a severe wildfire season--90-95 percent of which, by the way, are started by lightning.</P>
<P>Before we let the officials pass the blame onto these hapless individuals, or even simply on the presence of lightning, let's look at whether there aren't human-made conditions that have contributed to the problem.</P>
<P>According to a new study by the Bluewater Network, global warming--the raising of the earth's temperature as a result of greenhouse gases--is causing the dry drought conditions that make for more devastating wildfires.</P>
<P>The five-year wildfire average from 1996-2000 is the highest in 40 years, and this year's June "level 5" fire risk is the highest for this early in the fire season.</P>
<P>The cause of global warming? The unplanned nature of industrial capitalism, which, in the blind pursuit of profit, fails to take into account the unintended effects of the chemicals, gases and waste it produces.</P>
<P>But the story doesn't end there. Decades of rapacious logging has played its part. According to a report by the World Wildlife Fund, "In their natural state, many Western forests are comprised of large, old, fire-resistant trees spaced about 20-30 feet apart."</P>
<P>In the past, Indians used controlled burning to reduce the underbrush and smaller, less fire-resistant trees. This practice, which encouraged the size and spacing of trees described by the Fund report, made not only for better traveling and hunting, but also reduced the danger of catastrophic fires. The report argues, "Today, after a century of logging, livestock grazing, road building and indiscriminate fire suppression, Western forests are primed for intense fires."</P>
<P>As whites pushed the Indians off their land, forests, fallow fields and new forests that sprang up in clear-cut areas were allowed to grow into tangled forest thickets. The term "firestorm" was coined in the 1870s after a series of devastating fires began ravaging the Midwest. Sixty firestorms ripped through the region between 1870 and 1918, killing thousands of people.</P>
<P>The clear-cutting of the timber industry also left dried wood scattered around, often close to populated areas. Moreover, the deliberate suppression of fires around more populated areas only made fires more devastating once they broke out.</P>
<P>As the Wildlife Fund report states, "Fire suppression has disrupted the natural cycle of fire that cleanses the forest floor of young trees and other flammable plants which fuel catastrophic fires&#133;Bottom line--the body of scientific evidence shows that forests that are roaded, clear-cut, grazed and subjected to indiscriminate fire suppression are more prone to catastrophic, damaging fire."</P>
<P>Add to this budget cuts over the last two administrations that slashed the number, as well as the training, of firefighters, and we have a disaster waiting to happen. Everywhere we turn, in short, the priorities of capitalism--profit at any human, social or natural cost--helps create, or at the very least, exacerbate, the "natural" disasters we face.</P>

