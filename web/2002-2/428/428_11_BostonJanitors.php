<P><font face="Times New Roman, Times, serif" size="4">Boston janitors score a victory after four-week strike</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"We won respect"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By David Brophy</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 1, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>BOSTON--After a month of work stoppages and protests, janitors are expected to return to work this week after voting on a new contract. The Service Employees International Union (SEIU) Local 254 declared victory last week after reaching an agreement with major cleaning companies.</P>
<P>The package extends health care benefits to 1,000 more part-time workers and provides a pay increase of up to 30 percent over the next five years. By 2007, workers with more than five years experience will take home $13.10 an hour while others will earn $12.95.</P>
<P>Looking further ahead, the contract will expire at the same time as other big union contracts along the East Coast, placing the union in a much stronger bargaining position. Janitors have also won two days of paid sick-leave.</P>
<P>While these gains fall short of the workers' original set of demands, there is still plenty to celebrate in the aftermath of this groundbreaking strike. After 25 years of inactivity, SEIU has re-established itself as a prominent player.</P>
<P>More importantly, by taking to the streets for days on end, underpaid immigrant workers have made their presence felt throughout the city's corridors of power. "We won respect, and that's a lot," janitor Nicholas Duran told the <I>Boston Globe</I>.</P>
<P>The janitors also successfully fought off an injunction sought by Mayor Tom Menino against marching on the streets. "A lot of people overcame their fears about being politically active," according to SEIU organizer Aaron Bartley.</P>
<P>A rally last weekend brought the janitors' message to thousands of midday shoppers in the Prudential Center, where the cleaning contract is held by Janitronics--one of the corporations most hostile to the union's demands. Menino intervened to bring the parties to the table--not out of concern for the janitors' welfare, but to keep them off the streets.</P>
<P>At the SEIU victory rally, Menino was welcomed to the platform and praised by top union officials for showing "leadership." Leadership?! This is the same mayor who stalled the janitors' momentum by delaying their strike for months and then tried to ban their street marches in the courts. In reality, Menino wants to present Boston as a quaint backdrop for the 2004 Democratic National Convention.</P>
<P>With all their pro-worker rhetoric, you might think that the Democrats would be happy to associate themselves with a revival of union militancy in Boston. But they'd rather see the city's janitors vacuuming their convention center than have them picketing outside it!</P>
<P>In truth, the strike's success depended on the thousands of janitors who risked everything to win a better future for their families. "This is not the last time we're going to have to fight back," said Jos&eacute;, a janitor at the Prudential Center. "And the next time there's a strike, we can apply the lessons we've learned from this one."</P>
<P>As a result of the strike, "there's an understanding of the power that can be created by rank-and-file leadership," Bartley told <I>Socialist Worker</I>. From the outset, the campaign would have benefited from a greater emphasis on janitors organizing together to bring their coworkers out on strike--and supporting each other by maintaining strong picket lines. For Jos&eacute;, the lesson is simple: "We have to get everyone out next time."</P>
<P>Bartley argues that "there needs to be more democratic, rank-and-file involvement in the bargaining process." This means building on the network of rank-and-file representatives and picket captains that emerged from the strike to give janitors a stronger voice in the direction of their campaign. </P>
<P>We need to build on the example that the janitors have set for us by taking the energy of their strike into every struggle for justice--from the fight against the war on Iraq to the fight against the war on America's working poor. </P>

