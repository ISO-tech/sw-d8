<P><font face="Arial, Helvetica, sans-serif" size="2"><b>BRAZIL</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>What next after Lula's victory?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 1, 2002 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LEFT-WINGER Luiz In&aacute;cio "Lula" da Silva of the Workers' Party won Brazil's presidential election in a landslide last weekend. "This is&#133;an historic shift of direction, which shows how much this country wants change," said Candido Mendes, a leading Brazilian political analyst. "Lula has won in every region of the country and at every strata of society."</P>
<P>The former factory worker and union leader took 61 percent of the vote--the largest margin of victory in Brazilian history. His win puts a left-wing party at the head of Brazil's government for the first time in 40 years. Now Lula will face the immense challenge of meeting the high expectations of those who put him in office.</P>
<P>On the one hand, workers, landless peasants and the poor who voted for Lula expect him to follow through on his promises to increase wages, create jobs and fund programs to deal with the country's hunger crisis. Many also flocked to Lula because of his criticism of the U.S.-backed Free Trade Area of the Americas (FTAA)--and his pledge to reduce Brazil's economic and political subservience to U.S. bosses.</P>
<P>But on the other hand, a significant section of Brazil's elite also backed Lula--and expects something different. After years of free-market policies under President Fernando Henrique Cardoso that increased poverty and trapped Brazil's economy in stagnation, these bosses want a president who can maintain social peace while implementing austerity measures. Meanwhile, foreign investors who pulled billions of dollars out of Brazil in recent months and drove the value of the country's currency down by 38 percent in the last year are watching closely for any sign that Lula might consider defaulting on Brazil's massive debt.</P>
<P>For months now, Lula has gone out of his way to reassure the bankers that he's committed to paying off the debt--and that he's open to a "kinder, gentler" FTAA. For his running mate, Lula chose Jos&eacute; Alencar, an industrialist who hates unions and made a fortune supplying companies like Wal-Mart with dirt-cheap textile products--while paying his workers a pittance.</P>
<P>But despite these concessions to the business class, the currency markets have continued battering Brazil's economy. "Brazil's voters get to pick a president every four years," gloated the <I>Economist.</I> "The financial markets, by contrast, get to vote every day of the week. As a consequence, once the election is over, Mr. da Silva will have no time to waste&#133;It is only by trimming some of his promises--to raise the minimum wage, to spend more on social programs--that he might be able to assuage market fears. Yet it is policies in these areas which are, at least in part, what has swung votes towards him. Failure to deliver on these promises could harm him when he seeks another term."</P>
<P>From the moment Lula takes office in January, the potential of struggle from below against the effects of Brazil's crisis will loom around every corner.</P>

