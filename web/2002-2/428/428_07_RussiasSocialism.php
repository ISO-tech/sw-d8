<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>What happened to socialism in Russia?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 1, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>HASN'T SOCIALISM been tried--and failed? Didn't masses of people in Russia and Eastern Europe rise up a decade ago against governments that called themselves "socialist"?</P>
<P>The revolutions in Eastern Europe and the collapse of the ex-USSR have been cited many times as evidence of the "death of socialism." But the question depends, above all, on whether these societies should be called socialist. </P>
<p><a href="../../Featured/MarxDebate.shtml"><img src="../../Ads/MarxismDebateAdHome.gif" width="144" height="196" hspace="10" align="right" border="0"></a>
<P>In fact, the "rule of Marxism" in the USSR--not to mention imitators that still exist, like China and Cuba--was diametrically opposed to the basic principles of Marxism. If socialism is the "self-emancipation of the working class," as Karl Marx put it, then how could any society in which workers exercised no power be called socialist? </P>
<P>How does a society that tolerated and encouraged numerous forms of oppression--based on race, religion, gender, sexual orientation, nationality and ethnicity, to name a few--square with Marx's vision of "an association in which the free development of each is the condition for the free development of all"?</P>
<P>The answer is that the rulers of the former USSR used the rhetoric of Marxism to justify a different reality--an exploitative system, run by a minority, using forms of authority not that very different to capitalism in the West. </P>
<P>Such a masquerade isn't so odd. After all, we live in a country where half of political power lies with a party that calls itself Democratic--though its commitment to Corporate America means that it regularly acts in the most undemocratic ways. </P>
<P>This isn't to say that the Russian Revolution was a dictatorship from the beginning. No serious observer in 1917 doubted that masses of Russian workers and peasants supported the overthrow of the hated Tsar--and the establishment of the workers' councils, or soviets, as the basic form of government. The soviets gave the majority of people a way to have a democratic voice in controlling society. But could this system survive?</P>
<P>Marxists have always believed that the basis for socialism is abundance--having enough to go around. Certainly, such a society would be impossible if an economically backward country like Russia remained isolated in a sea of capitalism--without socialist revolutions in other countries to come to its aid. All of Russia's revolutionaries accepted this.</P>
<P>"We are far from having completed even the transitional period from capitalism to socialism," Lenin wrote in January 1918. "We have never cherished the hope that we could finish it without the aid of the international proletariat."</P>
<P>This wasn't a pipe dream. The years following the Russian Revolution saw massive upheavals in Germany, Italy and other European countries. But none succeeded in putting a workers' government in power. Russia was left isolated, and ultimately, Lenin was proved right--the revolution couldn't survive in these conditions.</P>
<P>But the form that its defeat took was unexpected. The Bolshevik-led workers' state defeated a civil war to topple it, but the basis for a society controlled by workers was wiped out. The Russian working class--the class that made the revolution--was effectively destroyed.</P>
<P>In these desperate conditions, the Bolshevik Party came increasingly to substitute its own rule for that of the decimated institutions of workers' power created out of 1917. Though not unopposed, Joseph Stalin and his fellow bureaucrats took charge as a new ruling class, carrying out a counterrevolution that reversed all the gains of 1917.</P>
<P>Was the Russian Revolution doomed to degenerate? The answer to this question is yes--if the revolution remained isolated. The hope of socialists today--in a world where the resources to meet people's needs are vastly greater--is for an international revolution that will free all humanity.</P>
