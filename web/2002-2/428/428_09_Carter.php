<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The real history of a Nobel winner</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 1, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>A SPOKESPERSON for the Nobel Prize Committee admitted that giving former President Jimmy Carter the Nobel Peace Prize amounted to a backhanded slap at the current war-mongering occupant of the White House.</P>
<P>Indeed, Carter's selection as Nobel laureate has sent liberals into a tizzy. Some seem ready to nominate Carter for sainthood. "He said as a candidate that he would never lie to us, and he didn't. He is the last to have made this pledge and kept it," wrote Marie Cocco of <I>Newsday</I>. "Carter is also the last president who appealed not to voters' self-interest, but to a selfless goodness he saw in the American people."</P>
<P>Excuse me if my eyes don't mist over at the news of Carter's award. As I remember him, the "peacenik" Carter was the president who brought back the military draft in 1979.</P>
<P>Carter's care and feeding of the U.S. war machine went far beyond bringing back the draft, whose abolition represented a victory of the movement against the Vietnam War. In the last two years of his administration, Carter increased the U.S. military budget by 10 percent--beginning the Pentagon buildup that Ronald Reagan would take to then-unprecedented heights.</P>
<P>All this followed the Russian invasion of Afghanistan. Carter used the "Soviet threat" to whip up support for the military budget. Years later, National Security Adviser Zbigniew Brzezinski admitted the Carter administration had armed Afghan insurgents to provoke a Soviet invasion.</P>
<P>In other words, the New Cold War whipped up in 1979-80 was based on a lie from the man who said that he'd never lie to us.</P>
<P>He created the Rapid Deployment Force (RDF) to place U.S. troops and materiel in the Middle East. The RDF evolved into the U.S. Central Command--which led 1991's Desert Storm and will run Bush's planned invasion and occupation of Iraq.</P>
<P>Today's Carter boosters contrast the Bush administration's talk about nuclear first strikes with Carter's supposed dedication to peace. But in 1979, Carter signed Presidential Directive 59, establishing plans for fighting a "limited" nuclear war, including a first-strike policy. Carter committed the U.S. to building the MX missile, a nuclear weapon that could be mounted on mobile launchers.</P>
<P>"Human rights" played a big role in Carter's rhetoric about U.S. foreign policy. But not its practice. The biggest gap between word and deed came in Washington's total support for the Shah of Iran, the brutal dictator who acted as the U.S. strongman in the Gulf.</P>
<P>In 1977, during a state visit to Iran, Carter toasted the Shah as an "enlightened monarch who enjoys his peoples' total confidence." Less than two years later, the Iranian people overthrew the Shah.</P>
<P>Another Carter favorite was Romanian dictator Nicolai Ceausescu, who won praise and Western aid for abiding by Carter's boycott of the 1980 Moscow Olympics. In 1989, Ceausescu and his hideous regime met the same fate that the Shah's did.</P>
<P>To many, Carter's greatest achievement for "peace" was brokering the 1978 Camp David Accord that resulted in Egypt's recognition of Israel. In fact, the U.S. designed the Camp David Accord to bolster Israel, by removing Egypt as a military challenger. Israeli hawks openly admit that the peace treaty with Egypt allowed Israel to concentrate its forces for its 1978 and 1982 wars in Lebanon.</P>
<P>No thought of justice for the Palestinians entered into Carter's considerations. In fact, one year later, Carter fired United Nations Ambassador Andrew Young for meeting with a member of the Palestine Liberation Organization.</P>
<P>In the years since he left the White House, Carter has constructed a public persona calculated to airbrush his disastrous presidency from history. Winning the Nobel Prize is the ultimate recognition of just how successful he has been in making millions forget that he started the New Cold War.</P>

