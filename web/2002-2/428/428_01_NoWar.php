<P><font face="Times New Roman, Times, serif" size="4">Protesters tell Bush:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>We don't want war on Iraq</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 1, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>PEOPLE ACROSS the U.S. joined antiwar activists around the world last weekend in resounding opposition to George W. Bush's war drive against Iraq.</P>
<P>On October 26, well over 100,000 people rallied in Washington, D.C., while 75,000 turned out in San Francisco, and thousands gathered in Seattle, Denver, Chicago, as well as cities across the country. That same day, 30,000 protested in Berlin, and thousands demonstrated in Rome, Madrid, Copenhagen and Stockholm.</P>
<P>October 26 marked the first national mobilization against Washington's plans for a new war on Iraq. It brought together everyone from veterans of the movement against the Vietnam War to new activists who have just begun to organize.</P>
<P>For Shane Johnson, a member of Ironworkers Local 498 from Rockford, Ill., this was his first protest. "I'm tired about the way things are going, and it seems like around our town, no one speaks out," he told <I>Socialist Worker</I>. "I wanted to be part of the movement. I decided I needed to do something."</P>
<P>Brad Kindler from the University of Nebraska was one of 50 members of Nebraskans for Peace who made the 24-hour drive to Washington. "It's definitely a war for oil," Kindler said. "It's not to disrupt terrorist networks and it's not to protect U.S. citizens."</P>
<P>As activists showed their opposition, the Bush gang was underlining their determination to go to war--no matter what. George W. Bush made that clear to other countries on the United Nations (UN) Security Council when he declared, "If the UN won't act, if Saddam Hussein won't disarm, we will lead a coalition to disarm him." That's why organizing opposition to Bush's war, especially here in the heart of the beast, is so important.</P>
<P>Saturday's protests show the immense opportunities that exist for building a movement against Washington's war drive if we organize in our schools, workplaces and neighborhoods--everywhere we are.</P>

