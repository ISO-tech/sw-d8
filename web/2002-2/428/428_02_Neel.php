<P><font face="Times New Roman, Times, serif" size="5"><b>When protesting becomes a crime</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Tina Gross</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 1, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"THE BUSHES must truly love the poor, they've made so many of us." That was the sign retired steelworker Bill Neel was arrested for carrying when George W. Bush came to Pittsburgh last Labor Day. This week, Neel will have a court hearing on the charges filed against him--which are nothing more than an attempt to make it a crime to protest.</P>
<P>Neel and his sister Joyce went to protest Bush's speech at the Carpenters' union headquarters. When they arrived, they discovered that police were herding protesters behind a fence far from where the president would appear. "It was like being in a prison--police all around the area," Neel told <I>Socialist Worker</I>. "You were isolated from anything that was even near the public."</P>
<P>Neel refused to get inside the sealed-off area--and held his sign outside a fence, where it could be seen from where the president's motorcade would pass. "The policeman says to me, 'You have to get back into the First Amendment zone,'" Neel remembers. "I said, 'I am not going to go back there.' I'm simply not going to do that. The idea that there's a First Amendment zone is a contradiction in terms." Neel was arrested and charged with disorderly conduct. </P>
<P>On October 21, Neel spoke at an antiwar rally in Pittsburgh and invited protesters to sign a recreation of his confiscated Labor Day sign--which he plans to mail to Bush to send him the message that he'd missed in Pittsburgh.</P>
<I><P>Activists are rallying in support of Bill Neel at his hearing on October 31. For more information, call 412-361-3022.</P>
