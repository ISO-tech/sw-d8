<P><font face="Times New Roman, Times, serif" size="5"><b>A candidate in New York worth voting for</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 1, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>, </P>
</B><P>There's currently an election campaign going on for the governor of New York state that makes you want to run screaming from politics. Carl McCall, a Democrat, and incumbent Gov. George Pataki, a Republican, offer nothing that will improve the lives of working-class and poor people in the state. </P>
<P>Pataki has overseen a massive rise in the gap between rich and poor and the crumbling of social services at a time when the state was in boom times. Several of the leaderships of the city and state's biggest unions have disgraced themselves by endorsing him.</P>
<P>McCall's response? Promise to cut social services without raising taxes and criticize Pataki for fiscal mismanagement. </P>
<P>However, there is a candidate worth voting for. Stanley Aronowitz, running as a Green Party candidate, is openly against the war on Iraq. In addition, he is for full repeal of the racist Rockefeller Drug Laws, increasing taxes on corporations and for repeal of the vicious anti-union Taylor Law. This is a platform that speaks to the actual priorities of millions of people. </P>
<P>While the Aronowitz campaign has not engendered the kind of involvement and profile that the presidential campaign of Ralph Nader did in 2000, it still can play a role in the rebuilding of the left. It gives an antiwar and anti-corporate alternative to people who are disgusted with the choices presented--and it lets people know that they are not the only ones who are angry and to the left of the mainstream offerings. </P>
<P>For these reasons and more, the membership of the International Socialist Organization in New York City has voted to endorse Aronowitz for governor. </P>
<B><P>Meredith Kolodner,</B> New York City</P>
<I><P>Visit www.stanleyaronowitz.org for more information.</P>
