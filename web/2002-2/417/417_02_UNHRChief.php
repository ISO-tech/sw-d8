<P><font face="Times New Roman, Times, serif" size="5"><b>U.S. gets human rights chief booted</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 9, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MARY ROBINSON is losing her job--because the U.S. government wants her out. "I am not somebody just to walk away," said the former United Nations (UN) High Commissioner for Human Rights, one week after UN Secretary General Kofi Annan announced her replacement. "[T]here seems to have been strong resistance from just one country." </P>
<P>One guess as to which country Robinson is talking about. Robinson, a former president of Ireland, is no radical. But she consistently annoyed the Bush administration by refusing to stay silent about human rights violations committed in the name of the U.S. "war on terrorism."</P>
<P>"Governments are using [the war on terror] to clamp down on human rights and freedom of expression--human rights defenders branded as terrorists; the harsh climate for asylum seekers and refugees," Robinson told <I>Salon </I>magazine. "Just look at the treatment of prisoners in Guant&aacute;namo Bay, and even more so those who have been arrested under immigration laws with no access to lawyers and no information. Nobody knows exactly what his or her situation is."</P>
<P>Robinson's replacement, UN diplomat Sergio Vieira de Mello, will be quieter. "He's a very diplomatic operator, somebody who doesn't run afoul of the big powers," one UN official told Britain's <I>Guardian</I> newspaper. When asked if de Mello would avoid a confrontation with the U.S. government, the official said: "The short answer is yes, and the long answer is yes."</P>

