<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>How Lincoln came to be an abolitionist</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 9, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"JUST AS one does not judge an individual by what he thinks about himself, so one cannot judge such a period of transformation by its consciousness, but, on the contrary, this consciousness must be explained from the contradictions of material life." So wrote Karl Marx in his "Preface to a Critique of Political Economy."</P>
<P>The American Civil War fits this description well. In the beginning, people believed that they were fighting for one thing, and ended up fighting for something else altogether. Lincoln began by proclaiming it purely a war to retain the union, to force the South to rejoin the United States and reestablish what secession had torn apart--without touching slavery.</P>
<P>This was hardly possible. The Southern states, under the leadership of the slave-owning planter aristocracy, seceded from the union precisely over slavery.</P>
<P>The pre-Civil War setup had allowed the Southern states to dominate politically in the North. In order to retain the union, Northern politicians bent over backward to accommodate slave owners, passing several compromises, including the Missouri compromise of 1820, which made Missouri a slave state.</P>
<P>But as the U.S. expanded westward, the issue of whether new states would be based on slavery or "free-soil" became a hotly contested issue, leading to bloodshed in Missouri and Kansas. The slavocracy needed new land because of the way in which the plantation system exhausted the soil--their economic viability depended on conquering new territory. But they also needed new slave states in order to retain their political dominance.</P>
<P>Lincoln's victory in 1860--on the Republican Party's no slave expansion ticket--sounded a clear warning to the Southern planter class that slavery's days were numbered, economically and politically.</P>
<P>The South seceded not to be "left alone," but to protect, and expand, slavery. Hence, they expended every effort to win over or conquer by force the so-called "border states" where there were more freeholders and fewer slave owners, and, they hoped, to seize more Western territory.</P>
<P>Once the war began, therefore, it slowly took on a logic and force that went beyond the limited bounds set by Lincoln and others. The conflict was not based upon misunderstanding or some mythical sense of Southern nationalism, but upon a clash between two social systems--slavery and free wage labor.</P>
<P>The Northern bourgeoisie was at first tentative, slow and afraid of turning the war into a revolutionary war. Most of them preferred compromise with the Southern ruling class rather than all-out war. This was reflected in the fact that the first commander of the Army of the Potomac, George B. McClellan, fought the war as if he expected a gentleman's agreement between the two sides to keep slavery happily intact.</P>
<P>But Lincoln and others slowly realized--as abolitionists like Frederick Douglass had already--that things must go forward not backward. If, in the past, Northern politicians opposed abolition in order to preserve the union, now they saw that they had to favor it in order to restore the union.</P>
<P>As historian Bruce Catton argued, slavery's destruction was "a revolutionary change embraced reluctantly." But embraced it was. For example, McClellan and others were replaced by generals who were willing to carry the fight to the end.</P>
<P>Lincoln's Emancipation Proclamation in 1862 and his decision to arm and train former slaves as soldiers signaled the transformation of the war into a revolutionary battle against slavery. Lincoln began by offering limited support for slavery; then proposing limited emancipation; and finally, by supporting complete emancipation and full citizenship rights for Blacks.</P>
<P>He was indeed a reluctant revolutionary! He said of his role during the Civil War, "I claim not to have controlled events, but confess plainly that events have controlled me."</P>

