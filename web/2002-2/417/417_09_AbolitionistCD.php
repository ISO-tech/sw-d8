<P><font face="Times New Roman, Times, serif" size="5"><b>Songs to stop the executioner in his tracks</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 9, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>MUSIC:</B> The Pine Valley Cosmonauts, <B><I>The Executioner's Last Songs,</B></I> Vol. 1. Bloodshot Records, 2002.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>"THE PINE Valley Cosmonauts consign songs of Murder, Mob-Law &amp; Cruel, Cruel Punishment to the realm of Myth, Memory and History." So reads the title of the liner notes of <I>The Executioner's Last Songs,</I> a benefit CD for the Illinois Death Penalty Moratorium Project. It's an accurate description of the 18 songs, ranging from traditional folk and country tunes to originals written for the collection.</P>
<P>The CD brings together the Pine Valley Cosmonauts with artists from the independent roots-rock label, Bloodshot Records. Covers of tunes by Hank Williams, Merle Haggard and Bill Monroe share the bill with a cover of the cult punk song "Gary Gilmore's Eyes." </P>
<P>The collection's most affecting original tune is "The Hangman's Song," a haunting duet by Puerto Muerto's Christa Meyer and Tim Kelley, whose bazooki accompaniment gives it a Balkan sound. "The Hangman's Song" records the fears of condemned prisoners ("Pray your neck breaks when the rope is taut/Pray your mother isn't there to see") and places a curse on the hangman who will execute them.</P>
<P>Jenny Toomey's reworking of Cole Porter's "Miss Otis Regrets" is the collection's most interesting track. Written in the form of a formal RSVP for a missed lunch date, the song tells the story of Miss Otis, who "regrets she's unable to lunch today"--because she killed her two-timing lover and then died at the hands of a lynch mob. Toomey's interpretation of the song reveals another side of Porter, who is usually known for his light-hearted Broadway standards.</P>
<P>The songs also portray the humanity of death row prisoners. Chris Ligon's "The Great State of Texas" is composed as a prisoner's last statement to his partner, reminding her of all the ordinary things they once shared, like working on the car or listening to Beatles' albums. But this is over, because "the great state of Texas has taken my life."</P>
<I><P>The Executioner's Last Songs</I> is a heartfelt contribution to the movement to abolish the death penalty.</P>

