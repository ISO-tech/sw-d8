<P><font face="Times New Roman, Times, serif" size="5"><b>Stop the Nazis in D.C.</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Maurice Alvarado</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 9, 2002 | Page 10</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WASHINGTON--The neo-Nazi National Alliance (NA) is planning a major mobilization for an August 24 rally at the U.S. Capitol. This follows a major showing in May when about 250 racists picketed the Israeli Embassy here. The NA claims that the rally will be the "largest gathering of white nationalists at the U.S. Capitol since World War II."</P>
<P>This is the sixth rally in the past year held by the NA in Washington, D.C.--and is part of an uptick in neo-Nazi activity in the D.C. metro area. The rally is intended to raise the confidence of far-right organizations by uniting the well-financed NA leadership with its base of support--the skinhead thugs who will be drawn to a "Rock Against Israel" concert following the rally.  </P>
<P>Since 9/11, the NA has cynically portrayed itself as sympathetic to the Palestinian cause as a cover for their hatred toward Jews and all people of color.</P>
<P>Ignoring violent racists will not make them go away. We need to reach out in a display of unity and diversity and show the Nazis that they're not welcome in D.C.</P>

