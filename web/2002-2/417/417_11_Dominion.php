<P><font face="Times New Roman, Times, serif" size="5"><b>Dominion Virginia Power</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jeff Bale and Jesse Hagopian</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 9, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>RICHMOND, Va.--Some 3,500 workers from Dominion Virginia Power went on strike for the first time in 38 years to defend their retirement and health care benefits from the corporate ax.</P>
<P>Members of International Brotherhood of Electrical Workers (IBEW) Local 50 walked the picket line at facilities in Virginia, West Virginia and North Carolina. Management provoked the strike last week when it walked away from the negotiating table after proposing an insulting final offer that included upping the worker's contributions for health care coverage.</P>
<P>Dominion has painted the IBEW as greedy for refusing a 16.5 percent wage increase over five years. But they don't mention that this barely keeps up with inflation. Even worse, management wants to increase both the age and years of service for workers to be eligible for retirement. So, for example, Ralph Quayle--a strike captain at the Dominion facility in Alexandria, Va.--says that he would have to work several years longer before retiring, even though he's already clocked 27 years.</P>
<P>Quayle said that workers were basically satisfied with the contract they had before--and had even agreed twice to two-year extensions at the company's request. </P>
<P>Despite profits of $75 million last year, Dominion is demanding concessions--while CEO Thomas Capps just gave himself a $4 million bonus. "Thomas Capps is just like Ken Lay," said one picketer. "He's part of this corporate crime wave, whether they say it or not. He gets away with stealing money out of our pockets, while we work harder for less."</P>
<P>The company's strategy is to try to get workers to scab. Not 15 minutes after workers left the picket lines on day one of the strike, management used its automated phone system to call workers at home, leaving a message inviting them to return to work, including an elaborate plan for avoiding picketers. The company has also hired private contractors and used supervisors during the walkout. </P>
<P>Not surprisingly, with this strike affecting one of Washington's power corridors, Bush has been pressuring Dominion's bosses to settle the strike and get the power back on. The power workers and their supporters have to keep up the pressure.</P>

