<P><font face="Times New Roman, Times, serif" size="4">West won't aid Africa</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The famine they want to ignore</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 9, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>COUNTRIES ACROSS Southern Africa are facing widespread famine--and the leaders of the West are doing nothing.</P>
<P>Some 13 million people in Malawi, Zimbabwe, Mozambique, Zambia, Swaziland and Lesotho are on the brink of starvation. Officials from the United Nations (UN) World Food Program say that at least $600 million is needed immediately to prevent the worst of the disaster--but Western governments have promised only a fraction of that. The European Union (EU) even had the gall to demand that Malawi's government return $8 million in development aid because of financial mismanagement.</P>
<P>But the real culprit isn't corruption in Africa. It's the barbaric free-market policies championed by the West--and forced on poor countries by the International Monetary Fund (IMF) and World Bank.</P>
<P>In July, IMF Managing Director Horst Koehler admitted that the World Bank and EU had pressured Malawi into selling 28,000 tons of grain in order to make a debt repayment earlier this year--just as the food crisis was beginning.</P>
<P>Austerity measures and free-market "reforms" demanded by the IMF and World Bank have wreaked havoc across Africa. In Malawi, the terrible twins of international finance demanded that the government reduce subsidies to farmers and price supports that made basic goods cheaper. The free market was supposed to fill the gap and make sure that people didn't starve to death. But it hasn't.</P>
<P>Meanwhile, Malawi and the other countries of sub-Saharan Africa continue to suffer under a devastating burden of international debt. Debt repayment from Africa to Western banks and international agencies amounts to a staggering $15 billion a year--25 times what the UN believes is needed to stop the famine in Southern Africa today.</P>
<P>Because of this debt burden, African governments are under the thumb of the IMF and World Bank. As Ann Pettifor, director of the debt abolition group Jubilee 2000 Coalition UK put it, "[W]e should be doing all we can to end the famine and provide food. But we should also be campaigning for greater economic justice for poor country debtors. This means liberating them from the structural injustice of international debt, where creditors play the role of policeman, lawyer, plaintiff, judge and jury."</P>
<P>The U.S. government has the resources to end the suffering in Southern Africa immediately. But Washington doesn't care about human lives--only the profits of its big business and banker buddies.</P>

