<P><font face="Arial, Helvetica, sans-serif" size="2"><b>LATIN AMERICA</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The misery of the free market</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 9, 2002 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ECONOMIC CRISIS and mass protests are spreading like wildfire across South America--from the continent's biggest power, Brazil, to smaller countries like Peru and Uruguay.</P>
<P>In Uruguay, officials closed all banks last week in a desperate attempt to stave off a financial collapse, after depositors--fearing a freeze on accounts--withdrew as much as they could. By the middle of the week, protesters had taken to the streets, looting supermarkets in the capital of Montevideo. </P>
<P>On Thursday, workers staged a general strike. "The strike was unanimously approved by the 42 unions we represent," said union leader Juan Castillo from the PIT-CNT, Uruguay's largest labor federation. The government cracked down, mobilizing 5,000 police and two air force helicopters to patrol the city.</P>
<P>Uruguay, once known as the "Switzerland of Latin America" for its sturdy financial system, has been hard hit by the economic crisis gripping Argentina and Brazil. Last year in Argentina, a rush of depositors withdrawing their savings touched off a financial meltdown that sparked popular protests, toppling five presidents in a matter of weeks. Since then, Argentinians who had money in Uruguayan banks have withdrawn about $3 billion--and in the process exported the crisis to their next-door neighbor.</P>
<P>With international investors pulling funds out of Latin America, even the strongest economy, Brazil, has been hammered. Last week, Brazil's currency, the <I>real,</I> hit record lows against the dollar for nine straight trading days, losing about 20 percent of its value in the past month. U.S. Treasury Secretary Paul O'Neill poured gas on the fire last week when he arrogantly declared to reporters that he opposed further loans to Brazil because he feared the money would "go out of the country to Swiss bank accounts."</P>
<P>The comment sparked fury among Brazilians--and left the White House scurrying to contain the damage. Brazil then sent a delegation to Washington to plead with International Monetary Fund (IMF) officials for a new multibillion-dollar loan to help contain the crisis.</P>
<P>IMF officials said that they would consider new loans--but took the opportunity to demand that Luiz "Lula" In&aacute;cio da Silva, the left-wing Workers' Party leader who is ahead in polls for the country's upcoming presidential election, promise to adhere to "sound" economic policies. That means more privatization and government budget cuts.</P>
<P>In fact, Brazil, like Argentina before it, has been a star IMF pupil for years. Washington's demands for austerity are responsible for the current crisis. And they have come at an enormous cost, driving millions into poverty across Latin America.</P>
<P>Even mainstream economists are talking about a replay of Latin America's "lost decade," when economic growth basically stagnated throughout the 1980s. So what do they recommend to regain the rapid growth of the early 1990s? More free-market "reforms."</P>
<P>But mass protests across Latin America show that millions are rejecting the IMF's dogma and the worship of the free market. Just as crisis is spreading from country to country, so is the mood of resistance.</P>

