<P><font face="Times New Roman, Times, serif" size="5"><b>Abandoned by the U.S. to justify a war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 9, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>A lot of folks find it beyond belief to think that the U.S. government might sacrifice thousands of American citizens to get an excuse to go to war. But a recent lawsuit filed by Northwestern University law professor Marcia Fee Achenbach and 500 other plaintiffs begs to differ.</P>
<P>Achenbach was one of 7,000 Americans stranded in the Philippines prior to the Second World War. Her lawsuit says that the government refused passport and travel documents to people who wanted to return to the U.S.--to use them as bait and justify declaring war on Japan.</P>
<P>Achenbach's family spent the duration of the war in a Japanese-run internment camp. When the camp was "liberated" by U.S. troops when she was 4 years old, Achenbach recalls "having to run around to get away from the shelling." </P>
<P>During research at the National Archives, Achenbach came across a letter from the High Commissioner of the Philippines' secretary, which confirms that officials were told not to issue travel documents.</P>
<P>Some people claim that the U.S. didn't allow Americans to leave because it didn't want the Filipinos to feel "abandoned." This is hogwash. This same government killed hundreds of thousands of Filipino freedom fighters to establish a colony there in the first place. The U.S. never gave a damn about Filipinos--just as it didn't give a damn about the U.S. citizens it abandoned there.</P>
<P>All the U.S. gives a damn about is power. Remember Achenbach the next time that Bush sheds crocodile tears about the victims of September 11.</P>
<B><P>Adam Turl,</B> Chicago</P>

