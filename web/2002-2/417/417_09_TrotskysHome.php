<P><font face="Arial, Helvetica, sans-serif" size="2"><b>A PEOPLE'S VACATION GUIDE</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Visiting Leon Trotsky's last home in exile</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>A rare glimpse into a revolutionary's life</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sherry Wolf</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 9, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MEXICO CITY is a favorite vacation destination for U.S. travelers able to go abroad. If you're there, a subway ride will take you to Coyoacan, the last home in exile of Russian revolutionary Leon Trotsky.</P>
<P>Trotsky, the leader of the Red Army, was exiled by Joseph Stalin in 1928. In 1937, after years of wandering from Turkey to Norway and France, Trotsky settled in Coyoacan.</P>
<P>Trotsky's home has been preserved as a museum. Those repulsed by the iconography of Stalin's Russia, which mocked great revolutionaries by transforming them into meaningless statues, will be pleased. Rather than a shrine, Trotsky's home is a testament to a man who fought to preserve the Marxist traditions of internationalism and socialism from below. </P>
<P>Because Trotsky was a hunted man, shunned by most countries of the West and targeted by Stalin's followers, he was forced to live inside a modest compound surrounded by high walls and guarded by armed men.</P>
<P>Mexican muralist Diego Rivera, a Mexican Communist Party founder, used his influence to gain him asylum. Rumors of an affair with Rivera's wife, artist Frida Kahlo, as well as ideological differences, separated the two friends in 1938.</P>
<P>Days after 20,000 Stalinists marched on May Day 1940 under the banner "Out with Trotsky," an attempt was made on his life, which left bullet holes that pockmark the wall above his bed. Trotsky jokes in a letter thanking a comrade for a gift four days before his assassination: "The bullet-proof vest has been piously admired by everyone."</P>
<P>Despite the fortress-like atmosphere, a Spanish communist befriended Trotsky's secretary, breached security and attacked Trotsky in his study with an ice pick. Trotsky died on August 21, 1940.</P>
<P>Trotsky's study is filled with economic and philosophical texts, and his desk is covered with international newspapers, a French book <I>The Coming War,</I> and a draft of the biography of Stalin he was writing at the time of his murder.</P>
<P>A photo exhibit shows Trotsky enjoying picnics and fishing expeditions with friends, such French surrealist writer Andr&eacute; Breton and Farrell Dobbs, a leader of the 1934 Minneapolis Teamsters strike. Those, along with the images of him hiking or feeding chickens, provide a rare peek into Trotsky's personal life.</P>
<P>Just a few blocks away is the museum home of Diego Rivera and Frida Kahlo. Not too much further down the road is the National Museum of Interventions. The three stops make for a great day's outing in Coyoacan.</P>

