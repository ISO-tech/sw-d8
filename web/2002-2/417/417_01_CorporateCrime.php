<P><font face="Times New Roman, Times, serif" size="4">Corporate criminals cash in</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The new robber barons</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 9, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THEY WERE called the "robber barons." At the end of the 19th century, they owned the corporations and the banks--and the politicians and the police. They made their own rules--and squashed anyone who got in the way of their accumulating huge fortunes.</P>
<P>Meet the new robber barons. They refused to be bound by rules, too--and profited handsomely from their corporate crimes.</P>
<P>There's Ken Lay, the former CEO of Enron, the energy giant that went belly-up late last year. In the three years before Enron collapsed into bankruptcy, Lay raked in $247 million. This won him the Number Three spot on the <I>Financial Times</I>' list of the "Barons of Bankruptcy"--corporate executives who cashed in big time even as the companies they ran were headed for disaster.</P>
<P>Global Crossing's Gary Winnick clocked in at Number One, grossing more then $512 million. Winnick and his fellow robber barons made bankruptcy pay.</P>
<P>Roy Rinard wasn't so lucky. After 21 years as a utility lineman for Portland General Electric (PGE), the 54-year-old Rinard has arthritis in his hip and sarcoidosis in his lungs. Last year, he was looking forward to retiring on the benefits from his 401(k) plan, which was invested in Enron, PGE's new corporate parent. Then Enron started spiraling toward bankruptcy.</P>
<P>Ken Lay had already sold huge chunks of his stock holdings. But Roy was barred from selling his Enron shares--and could only watch helplessly as his dreams for a comfortable retirement disappeared. "Now, after 35 years of climbing poles, I'm just right back to where I started," he says.</P>
<P>Now Washington is spewing rhetoric about getting tough on corporate criminals. But that will be hard for Bush and Co.--since they're married to the same mob that they claim they're going to punish.</P>
<P>And while Democrats may complain that Bush isn't doing enough about corporate greed, they won't push too hard. After all, plenty of them took Enron's money. And they aren't about to bite the corporate hand that still feeds their campaigns.</P>
<P>The Washington con men aren't going to fight to save workers' savings from the corporate raiders. We have to stand up to the corporate criminals--and their bankrupt friends in Washington.</P>

