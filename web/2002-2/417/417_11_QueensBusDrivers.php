<P><font face="Times New Roman, Times, serif" size="5"><b>Queens bus drivers vote on deal</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Meredith Kolodner and Monique Jean Dols</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 9, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Striking workers at private bus lines in Queens were set to vote on a contract offer as <I>Socialist Worker</I> went to press. The 1,500 strikers, members of Transport Workers Union (TWU) Local 100, have been on strike for almost two months.</P>
<P>Workers' solidarity during the strike pushed back concessions on health care benefits that management had demanded, and the deal will hold the line on pensions. In addition, workers will get a 9 percent raise over 27 months without any givebacks on productivity.</P>
<P>Strikers seemed satisfied that they had beaten back the attacks on health care, but some said they had hoped to win more job security after so long without a paycheck.</P>
<P>The union did secure resolutions from the city council, pledging that jobs, wages, benefits and collective bargaining rights will be maintained when the bus franchises go up for bidding by new companies. While this is more than they have gotten in the past, the resolutions are nonbinding and don't guarantee job security. For this, the union would need written agreement from Mayor Bloomberg--and the billionaire mayor of New York has refused to make this guarantee.</P>
<P>Bloomberg made concessions on health care after trying to appear aloof from the battle, one striker told <I>Socialist Worker</I>. "He tried to distance himself from the private lines and say it was a private matter," the striker said. "But then we would point up to the sign that said 'Department of Transportation, City of New York.' So if somebody was there with a camera, we would just point to that. I think he was backed into a corner on that one."</P>
<P>Both the union and city officials looked at the strike as a trial run for this fall's contract negotiations for 38,000 bus and subway workers, when Local 100 will again face off against Bloomberg and the Metropolitan Transit Authority (MTA).</P>
<P>Local 100 President Roger Toussaint, part of the New Directions reform slate that ousted a corrupt old guard, is selling the new contract as if it had no flaws. In reality, more was needed to win real job security.</P>
<P>While some basic solidarity work was organized, strikers should have received more support from other unions in this heavily unionized city. And the TWU's MTA workers weren't activated for solidarity rallies or actions that could have increased pressure on Bloomberg.</P>
<P>Last week, the union's old guard--led by Local 100 Vice President George Jennings--tried to take advantage of workers' legitimate concerns about job security. But two days before the contract was to be voted on, they backed down on their threat to call for a "no" vote since they have no real alternative.</P>
<P>Jennings--accused by many workers of running a racist, corrupt administration--at one stage tried to discredit Toussaint and break the private lines into a new local. But Toussaint has increasingly turned New Directions into his personal caucus--at the expense of union democracy and accountability to the rank and file. </P>
<P>Most workers seemed ready to vote in favor of the new deal, deferring the struggle over job security until March 2003. With the experience of this strike, rank-and-file workers in the private bus lines can begin to form a network of people who reject the old guard, but who are also prepared to push Toussaint to fight for real job security.</P>

