<P><font face="Times New Roman, Times, serif" size="5"><b>Kept out of college because of poverty</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 9, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>HUNDREDS OF thousands of high school seniors won't be going to college this fall--not because they aren't smart enough or didn't work hard, but because they can't afford to. That's the conclusion of a new report drafted by Federal Advisory Committee on Student Financial Assistance.</P>
<P>According to the study, more than 400,000 qualified high school students won't be able to attend four-year colleges this fall--and nearly 170,000 won't ever attend any college--because the cost is too great.</P>
<P>Researchers found that almost half of college-qualified low-income students were too poor to attend a four-year college this year. Almost a quarter will never attend any college at all for this reason.</P>
<P>Why? On average, according to the study, families of low-income students (with less than $25,000 in annual income) attending four-year colleges and universities had to fill a "gap" of $3,800 a year in tuition and fees not covered by grants or financial aid. The study's conclusion was blunt: "For these students, the promise of a college education is an empty one."</P>
<P>What's more, the problem is likely to get worse in the coming years. "[T]he financial barriers to a college education have risen sharply due to shifts in policies and priorities at the federal, state and institutional levels, resulting in a shortage of student aid, and, in particular, need-based grant aid, as well as rising college tuition," reads the report.</P>
<P>So what is George "My daddy bought my way into Yale" Bush going to do about this obscenity? Not much. The self-proclaimed education president is increasing funding for Pell grants--the federal financial aid program for low-income college students--but by such a tiny amount that the final total will actually represent a cut because of the large increase in students applying for the grants.</P>

