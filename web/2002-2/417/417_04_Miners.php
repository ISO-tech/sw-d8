<P><font face="Times New Roman, Times, serif" size="4">Miners' lives are worth nothing to the bosses</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>An unnatural disaster</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 9, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>I'm from western Pennsylvania, where coal mining has been a way of life for more than 200 years. Like many people, I cried with relief when the nine miners trapped in the Quecreek Mine were rescued. </P>
<P>But now that they're safe, it's time to ask: who's to blame for putting these workers' lives at risk? The media has reported the cause of the accident as "bad maps." But retired miner Joseph Jashienski, who worked in the now abandoned mine, told <I>Salon</I> magazine that his bosses carved out a large area in order to get a bigger yield--then falsified that area on the map, putting future miners' lives at risk.</P>
<P>People in Pennsylvania have suffered many mine disasters over the years--in 1904, my hometown of Cheswick lost 179 miners. Miners' families often accept this danger as a fact of life, but these disasters are not inevitable.</P>
<P>This mining company, Black Wolf Coal, a nonunion mine, has received 26 citations from the Mine Safety and Health Administration since the beginning of 2001. The government's punishment for endangering miners' lives? A fine of only $859. Is that what miners' lives are worth to the bosses? </P>
<P>After their rescue, Blaine Mayhugh, one of the trapped miners, told a press conference that neither he nor the other miners had received even so much as a telephone call from Black Wolf.  </P>
<P>Reactionaries like Michael Novak of the <I>National Review</I> gave credit for the miners' heroic rescue to the "faith" and "conservative virtues" of the people of Somerset County, which he declared the "Conservative Capital of the World." </P>
<P>What Novak fails to note is that while this conservative region--a local hub of the KKK--has a history of blatant disregard for mine workers' safety, it also has a history of workers fighting for safer working conditions, better pay, the right to organize unions and to show the bosses that they are human beings whose lives are valuable.  </P>
<P>This rescue, which the press has labeled the "Quecreek Miracle," wasn't due to the hand of God or to the conservatism of Somerset. It succeeded because hundreds of workers came together and pooled their knowledge and resources to save fellow miners.</P>
<P>This horrible "accident" is one more reason why we need to build for a socialist society that puts people's lives before profit.</P>
<B><P>Sarah Grey,</B> Pittsburgh, Pa.</P>

