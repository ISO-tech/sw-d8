<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Washington debates plans for a new war on Iraq</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The horrors they're ready to commit</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 9, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>NOT IF but when. That's the consensus of the Washington establishment about a barbaric new war on Iraq.</P>
<P>Last week, with the Senate Foreign Relations Committee holding hearings on the administration's war plans, the frenzied speculation about possible invasion plans hit new heights. Judging from the continual leaks to the media, the Bush administration seems divided between a full-scale invasion involving 250,000 U.S. troops; a campaign of carpet bombing supported by "Afghan-style" proxy forces inside Iraq; or--the newest plan, nicknamed the "inside-out" strategy--heading straight for Baghdad with a "slimmed-down" force of less than 80,000 troops.</P>
<P>Under the latest plan, U.S. forces would hit the Iraqi capital, along with one or two key command centers and weapons depots--with the goal of causing Saddam Hussein's government to collapse quickly. No one bothered to mention, of course, that a lightning strike on Baghdad, a city of 3 million people, would cause massive casualties.</P>
<P>The Bush gang doesn't care about the facts. Last week, for example, the Iraqi government invited United Nations (UN) weapons inspectors for talks, but the Bush gang predictably scoffed at the offer. "Our policy remains the same," said National Security Council spokesperson Sean McCormack. "It has been the same since 1995 and that is regime change."</P>
<P>You won't hear much different from the Democrats. They may raise a few questions in Senate hearings--but not about whether to invade, only how. "We all support strongly a regime change," said Senate Majority Leader Tom Daschle (D-S.D.) on ABC's <I>This Week</I> talk show. "But I think we have to get our ducks in order."</P>
<P>No one in Washington seems to care that there's not a shred of credible evidence to back up the claim that Iraq has the capability to manufacture chemical or biological weapons--let alone the supposed "nuclear arsenal" that some pro-war fanatics are claiming.</P>
<P>Several former UN employees--including ex-humanitarian aid coordinators Hans von Sponeck and Denis Halliday, and even former weapons inspector Scott Ritter--have repeatedly offered to testify to this.</P>
<P>Instead, the senators chose to listen to pro-war hack Anthony Cordesman, who proudly declared, "If we do this [invade Iraq], it will in many ways be our first pre-emptive war. We will not have a clear smoking gun."</P>
<P>The carnage from a new war on Iraq will be horrific. And that's on top of a decade of UN economic sanctions that have resulted in more than 1 million Iraqi deaths.</P>
<P>"The U.S. Department of Defense and the CIA know perfectly well that today's Iraq poses no threat to anyone in the region, let alone in the United States," von Sponeck told Britain's <I>Guardian</I>. "To argue otherwise is dishonest&#133;Why then, one must ask, does the Bush administration want to include Iraq in its fight against terrorism?" </P>
<P>The answer is that George W. Bush wants to finish what his father started in Iraq--protecting the U.S. government's ability to plunder oil resources in the Middle East, while also showing the world that Washington can go anywhere and do anything it wants.</P>
<P>But the Bush gang could find very quickly that Iraq is a step too far. Most opinion polls show majority support for a new war on Iraq, but the doubts and questions run just below the surface. The <I>New York Times</I> discovered this when its team of interviewers for an in-depth feature "found some people favoring a strike against [Saddam] to prevent him from using weapons of mass destruction against the United States and its allies. But many more argued against an American offensive. Democrats and political independents interviewed were nearly unanimous in their opposition to an invasion, and most Republicans felt the same way."</P>
<P>These voices of opposition may remain passive now, until the U.S. makes more concrete moves toward war. But they represent the growing number of people who are questioning Washington's barbaric wars.</P>

