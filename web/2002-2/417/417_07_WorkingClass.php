<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The working-class majority in the U.S.</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 9, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE PHRASE most associated with Karl Marx is, "Workers of the world unite--you have nothing to lose but your chains." For many people, that phrase alone relegates Marxism to irrelevance in the modern world--an antiquated notion that may have applied in Marx's time or even in poorer parts of the world today, but certainly has no relevance to wealthy societies like the U.S.</P>
<P>The image of U.S. society most of us hold is one with a tiny number of very rich and very poor at each end of the economic spectrum, and an enormous middle class in between. There is a good reason why this image is accepted by most people. It's planted there.</P>
<P>The consumption patterns and lifestyles of the middle class are taken as a model that, through sheer repetition in advertising, TV and movies, the Internet and other forms of popular culture, inundate us with the conventional wisdom that the U.S. is a "middle-class society."</P>
<P>The idea of class struggle would have no application in a society in which the vast majority is deemed far too concerned with getting ahead in the system to take an active interest in overthrowing it. But this image is a myth.</P>
<P>"Class is one of America's best-kept secrets," argues Michael Zweig, in his book, <I>The Working Class Majority</I>. The vast majority of people in the U.S. are part of the working class--as Marx defined it: those who do not own or control the means of production, and must sell their own labor power to survive.</P>
<P>The Marxist understanding of the working class is not based upon income, although working-class living standards in the U.S. have declined significantly since the 1970s. The working class is made up, generally speaking, of non-supervisory employees who do not control their own work process but rather whose work process is controlled by management.</P>
<P>According to the Bureau of Labor Statistics, non-supervisory employees make up 82 percent of the workforce in the U.S. Even subtracting non-supervisory employees who themselves are not supervised--professionals such as lawyers, accountants and most doctors--still leaves roughly 75 percent of the U.S. workforce in working-class jobs.</P>
<P>The working class includes people of all races and both genders (white male workers are actually a minority of all workers) working not only in factories and on construction sites, but in offices, hospitals, airlines, restaurants, department stores, supermarkets and classrooms.</P>
<P>Only 9 percent of families in the U.S. today live in the so-called "traditional" family--with Dad going off to work and Mom staying home with the kids. The vast majority of women are in the workforce. Secretarial work is the biggest job category for white women and Latinas, and the third biggest for Black women.</P>
<P>And the U.S. is hardly the land of opportunity that it's chalked up to be. Class position is largely determined at birth. The single most likely occupation for a child born in the U.S. is the occupation of his or her parent. A child from the richest fifth of families is 10 times more likely to go to college than a child from the poorest fifth.</P>
<P>Racism further prevents upward mobility for the mass of Blacks and other racial minorities, who are disproportionately represented in the working class. But as economist Doug Henwood has argued, "there's no shortage of awful jobs for white folks either."</P>
<P>Truck driving is the leading occupation for Black men, but the second biggest for both Latinos and white men. Janitorial work is the leading occupation for Latinos, second for Black men, and the sixth biggest for white men.</P>
<P>Most people in the U.S. already define themselves as part of the working class. In a 1996 <I>New York Times</I> poll, 61 percent called themselves either "working class" or "lower class," as opposed to middle class.</P>
<P>The working-class majority in the U.S. shares the same class interests as the working class in poorer countries around the world. Marxism has never been more relevant than it is today.</P>

