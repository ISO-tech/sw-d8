<P><font face="Times New Roman, Times, serif" size="4">More police won't stop inner-city violence</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Cops aren't the answer</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Terzakis</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 9, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"One hundred more cops don't make me feel safer." That was the message on the signs of demonstrators at the Oakland City Council meeting last week. They came to protest Mayor Jerry Brown's answer to violent crime--a plan to spend $65 million to hire 100 new cops.</P>
<P>Sixty-seven people have been murdered in the city of Oakland this year. Some 85 percent of the victims and 97 percent of known suspects were African Americans. Grieving and frightened, Oakland residents are understandably desperate to find a way to stop the violence, and many therefore support Brown's plan.</P>
<P>But hiring more cops doesn't address the root cause of the problem--poverty. Numerous studies show that high murder rates are caused by the lack of jobs in poor minority communities. Also, hiring more cops inevitably means more brutality. </P>
<P>Ironically, on the same day that the City Council was considering Brown's plan, it decided to pay out $115,000 to settle two excessive force cases against Oakland cops. Meanwhile, press reports revealed that Officer Clarence "Chuck" Mabanag--a member of a police gang called the Riders who is awaiting trial on charges of falsifying reports, planting evidence and beating suspects while handcuffed--had been brought up on excessive force charges before the Riders scandal broke. </P>
<P>A referendum on the November ballot will decide Brown's plan, which would pay for the added cops by hiking taxes on parking, hotels and utility bills. But more police won't solve the problem of inner-city violence. Money for schools, better access to higher education and better-paying jobs will.</P>
<P>And the truth is the money for all this isn't far away. The poor neighborhoods of the Flatlands lie at the feet of the Oakland Hills, where multimillion-dollar homes provide residents with stunning views of the San Francisco Bay. A real solution would involve taxing the rich.</P>

