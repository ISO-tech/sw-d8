<P><font face="Times New Roman, Times, serif" size="4">Deal falls short on wages, full-time jobs and more</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Vote no on UPS contract</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Donny Schraffenberger, member, Teamsters Local 705</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 9, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE MEDIA have praised the United Parcel Service (UPS) proposed contract with the Teamsters as a "win-win" deal for management and union members. On the surface, this deal doesn't seem to have the outright concessions seen in other recent contracts. But a closer look shows why this deal is bad for Teamsters--and should be voted down.</P>
<P>UPS has had five years of record profits. The company is like an ever-expanding pie, and keeping the status quo means that workers would get a relatively smaller piece over the course of the six-year deal. For example, while this contract would add 10,000 new full-time jobs, the percentage of part-time employees would still increase from the current 57 percent. And no full-time jobs would be created until the third year of the contract.</P>
<P>It's worth remembering that it took more than two years and national arbitration to force UPS to honor the 10,000 full-time jobs won through our 1997 strike. Even then, the jobs that UPS created paid less than other full-time work, such as package car and over-the-road "feeder" driving.</P>
<P>Similarly, in the tentative agreement reached by Chicago's Local 705--which has a separate contact, most of it modeled on the national deal--a minimum of 450 new full-time jobs would be created, starting the second year of a six-year deal. Last time, Local 705 won 600 new full-time jobs over five years. We shouldn't be going backwards. Some 7,000 part-timers work at Chicago's CACH facility alone.</P>
<P>Another major problem with the national contract is that Article 40, which deals with the highly profitable next-day air operations, remains almost the same. Top-scale combination full-time air drivers would continue to make at least $6 less an hour than package car drivers. Part-time air walkers would still start at $8.50 an hour in 2008, while a package car driver would make over $28 an hour. Not the best deal</P>
<P>The majority of Teamster members at UPS make less than $11 an hour today. But if the contract is approved, base start pay would remain $8.50 an hour and only go up 50 cents after 90 days.</P>
<P>Top-scale package car drivers would make more than three times the hourly wage of a newly hired unloader in 2008. And the jobs of feeder drivers would continue to be subcontracted to nonunion trucking companies and railroads.</P>
<P>How can Teamsters President James Hoffa call this the "best UPS contract ever?" The massive gap between top-scale full-timers and part-timers can only help management to divide us, and six years is far too long to wait for a new contract. </P>
<P>All UPS Teamsters should demand a contract meeting and organize members to vote no. We need to force the negotiating committee to get us a contract with more pay for part-timers--at least $12 an hour base pay--and more full-time jobs immediately. We deserve nothing less.</P>

