<P><font face="Arial, Helvetica, sans-serif" size="2"><b>AFGHANISTAN</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">UN buries a report exposing U.S. cover-up of bombing atrocity</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Victims of another Pentagon blunder</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 9, 2002 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"LESS THAN perfect intelligence." That's the excuse that the U.S. military is using to explain its slaughter of dozens of innocent Afghans last month.</P>
<P>In an overnight raid on July 1, U.S. warplanes bombed a wedding celebration in the village of Kakrak in the central province of Uruzgan. Although the Pentagon later claimed to only find evidence of five graves, casualty estimates by the Afghan government hovered around 50, and reporters on the scene said that dozens more were wounded and possibly killed.</P>
<P>Afghan government officials admitted that U.S. warplanes mistook wedding participants firing into the sky for anti-aircraft fire and swooped in for an attack. At the time, the Pentagon was reluctant to admit their blunder.</P>
<P>Fast-forward a month, and it's clear that the Pentagon is still unwilling to tell the truth. "War is not a precise art," Col. Roger King whined to reporters. "You may also have, from time to time, less than perfect intelligence upon which you're acting."</P>
<P>But according to the United Nations (UN), the bombing was more than an "intelligence failure." In late July, the <I>Times</I> of London reported that the UN was preparing to release a report showing that U.S military forces also tried to cover up their deadly massacre.</P>
<P>In addition to placing the death toll at 80, the draft UN report apparently shows clear evidence of human rights violations by U.S. military forces. According to the <I>Times</I>' account of the report, coalition forces arrived on the scene very soon after the air strikes--and "cleaned the area," removing evidence of "shrapnel, bullets and traces of blood." To top it off, the report says there is "no corroboration" that U.S. forces had been fired on at all.</P>
<P>But it now appears that the UN's preliminary report won't ever see the light of day. The day after its contents were leaked, the UN suddenly announced that it wouldn't release the report. Instead, it said that the report would be shown only to the U.S. and Afghan governments. As one UN official told the <I>Times</I>, "The more [that the investigation] drags on, the harder it is to prove, and probably the people investigating want it to go slowly and die away."</P>
<P>Some commentators have wondered why the UN would take the unusual step of investigating a U.S. military action in the first place. But the attack was so brutal--and the official response from the U.S. military so callous--that the incident outraged people across the globe.</P>
<P>Of course, that's typical of the Bush administration's "war on terrorism" in Afghanistan. In fact, just as the UN report was due to be released, the U.S. government's stooges in the new Afghan government released details of the capture of a man who reportedly attempted to detonate a car full of explosives in the capital city of Kabul.</P>
<P>U.S. officials scrambled to link the man to "al-Qaeda terrorists" and sent bodyguards to prop up and protect the regime of Hamid Karzai, the White House's hand-picked choice to run the country. But of course, no one in the Washington establishment has pointed out that the U.S. war machine is fueling this kind of backlash of anger across the globe.</P>
<P>As a recent report by the very mainstream Council on Foreign Relations describes, many countries around globe view the U.S. government as "arrogant, self-indulgent, hypocritical, inattentive and unwilling or unable to engage in cross-cultural dialogue." The Bush administration apparently recognizes the reality--and has hired a public relations firm to help spruce up America's "image" overseas.</P>
<P>But with the Pentagon killing the very people they claim to be "liberating" and then dragging their feet instead of apologizing, is it any wonder that people across the globe hate the U.S. government?</P>
<P>From cutting deals with thugs and warlords, to failing to deliver promised economic aid, to exploiting and then abandoning calls for women's rights, the U.S. government has shown that it will say and do anything to win its war on Afghanistan. Thousands of innocent Afghans have paid the price.</P>
<P>We have to say no to Washington's war makers.</P>

