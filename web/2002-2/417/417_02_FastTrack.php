<P><font face="Times New Roman, Times, serif" size="5"><b>Bush's fast-track fraud</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 9, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CONGRESS HANDED George W. Bush a major victory when it approved "fast-track" trade authority as <I>Socialist Worker</I> went to press.</P>
<P>Fast track gives Bush the ability to negotiate trade deals and requires Congress to vote them up or down, without amendments. U.S. multinationals badly want Bush to have fast track--so their buddy in the White House can negotiate more agreements like NAFTA that allow them to spread their influence throughout the Western Hemisphere.</P>
<P>Under pressure from organized labor, fast track was shot down in Congress repeatedly since 1994, when the last law granting such authority expired. But Democrats joined with Republicans to pass the legislation this time. So much for the party that claims to be the friend of working people.</P>
<P>Bush celebrated the vote as a victory for consumers--while carefully avoiding the facts about how free-trade agreements like NAFTA have hurt workers, both in the U.S. and abroad. To get the legislation passed, Bush agreed to a compromise that would give limited benefits to workers left jobless because of a trade deal. But it will be hard to qualify.</P>
<P>With fast track in hand, Bush hopes to push through the Free Trade Area of the Americas agreement that would create a regional trading bloc made up of every country in the hemisphere except Cuba.</P>
<P>But he'll have a harder time of it with protests against the free market spreading across Latin America.</P>

