<P><font face="Times New Roman, Times, serif" size="4">End U.S. support for Israel's terror</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Stop the war on Palestinians</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 9, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ISRAEL WAS poised to commit new horrors against Palestinians as <I>Socialist Worker</I> went to press. The Israeli military imposed a complete ban on Palestinian travel in the West Bank and used tanks to seal off part of the Gaza Strip Monday in retaliation for Palestinian attacks that killed 13 people in 24 hours.</P>
<P>Striking an ominous tone, Israeli Defense Minister Binyamin Ben-Eliezer declared, "We will continue with a long series of measures, which I cannot speak of now, whose aim is to implement a much wider closure than we are doing."</P>
<P>In the U.S., George W. Bush told reporters that he was "furious" about the Palestinian attacks. "There are a few killers who want to stop the peace process that we have started," said Bush, wagging his finger at reporters. "We must not let them."</P>
<P>What a hypocrite! When Israeli forces used U.S.-supplied F-16 jets to drop a one-ton bomb on a Gaza apartment building, killing 15 people and injuring more than 100, Bush couldn't muster any fury. Instead, his press secretary, Ari Fleischer scolded Israel for being too "heavy handed."</P>
<P>Despite the fact that Israel violated the law by using U.S.-supplied weapons against civilians, no politician in Washington even hinted at ending U.S. economic and military aid. Likewise, the Bush White House had nothing to say about a riot of settlers in the West Bank city of Hebron last week that left a Palestinian girl dead and nine more people wounded, including two 8-year-old boys.</P>
<P>Even a senior Israeli official who witnessed the rampage was sickened. "Dozens of thugs, including youths from Hebron, burst into Arab houses for no reason," said Moshe Givati, a chief adviser to the Israeli government on relations with settlers. "[The settlers] broke windows, destroyed property and threw stones. These people were there for the purpose of making a pogrom."</P>
<P>Israeli officials also tried to exploit the release of a United Nations (UN) report last week on the carnage in Jenin during last spring's takeover of the West Bank. They claimed that the report cleared Israel of widespread accusations that its forces committed a massacre. But Israel barred investigators from entering Jenin, so the report was based on secondhand accounts.</P>
<P>The UN did say that Israel used excessive and disproportionate force in civilian areas, blocked medical treatment for wounded civilians for days, prevented aid workers and journalists from reaching the camp and killed 52 Palestinians during its assault.</P>
<P>But the report should have called these acts of terror what they were--war crimes. "It's a cop-out for the UN to say that this is the best they could do under these circumstances," said Peter Bouckaert, a senior researcher for Human Rights Watch who led a fact-finding mission to Jenin in late April.</P>
<P>We have to stand up to U.S. backing for Israel's terror. We demand justice for Palestinians now!</P>

