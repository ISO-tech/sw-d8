<P><font face="Times New Roman, Times, serif" size="5"><b>The war that never ended</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 13, 2002 | Page 5</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE GULF War left Iraq shattered. But that didn't stop the U.S. from maintaining a strict economic embargo that had been imposed by the United Nations (UN) following Iraq's invasion of Kuwait.</P>
<P>"The recent conflict has wrought near-apocalyptic results upon the economic infrastructure of what had been, until January 1991, a rather highly urbanized and mechanized society," officials of the UN mission to Iraq wrote. "Now, most means of modern life support have been destroyed or rendered tenuous. Iraq has, for some time to come, been relegated to a pre-industrial age, but with all the disabilities of post-industrial dependency on an intensive use of energy and technology."</P>
<P>The truth is that the war on Iraq never ended. Twelve years later, the country is still suffering from the devastating sanctions. According to the United Nations Children's Fund, between 1990 and 1998, under-5 child mortality rates doubled in Iraq--leading to half a million "excess" deaths.</P>
<P>Many more died from easily preventable waterborne diseases and lack of adequate medical supplies. The sanctions have kept out chlorinators, fertilizers, vaccines, ambulances and other vital civilian goods--under the pretext that they could have a military "dual use."</P>
<P>And the bombing never stopped. U.S. and British planes taking off from Turkey and Kuwait have regularly bombed Iraq in a war that the corporate media have deliberately chosen to ignore. While the U.S. claims it is "enforcing UN resolutions," no resolution ever authorized the so-called no-fly zones.</P>
<P>Still today, the U.S. claims that all the suffering in Iraq is caused by Saddam Hussein, not the bombing or the sanctions. That's nonsense. As Tun Myat, administrator of the UN oil-for-food program in Iraq, told the <I>New York Times</I>, "People have become so poor in some cases that they can't even afford to eat the food that they are given free, because for many of them the food ration represents the major part of their income."</P>
<P>The embargo has created massive inflation in Iraq, wiping out the savings and earnings of the overwhelming majority of Iraqis. Unemployment has skyrocketed, and children now beg--or sell themselves for sex--in order to help feed their families.</P>
<P>Meanwhile, more than one-fourth of Iraq's limited oil revenue is diverted to pay reparations to the Kuwaiti government and multinational oil companies, and to finance the UN's operations in Iraq. To use what little is left, Iraq has to apply to the UN--where the U.S. uses its veto on the sanctions committee to routinely block requests for imports.</P>

