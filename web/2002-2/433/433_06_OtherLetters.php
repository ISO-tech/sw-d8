<p><a name="flag"></a><font face="Times New Roman, Times, serif" size="4"><b>We shouldn't wave the flag</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I></B></P>
<P>I disagree with Joe Grossman's view that "as long as Americans view their flag positively we should embrace it." (<I>SW,</I> November 22)</P>
<P>Workers in the U.S. have far more in common with the people of Iraq than we do with the flag-waving bigots who cut our budgets while they spend billions bombing our Iraqi brothers and sisters. Moreover, the use of the flag in the antiwar movement implies that the U.S. can play a good role in world affairs. One look at the U.S. record of mass slaughter shows this isn't the case.</P>
<P>As the American socialist John Reed put it: "Uncle Sam never gives anybody something for nothing. He comes along with a sack stuffed with hay in one hand and a whip in the other." This applies particularly to workers in the U.S. who are being told that they should be patriotic and accept pay cuts and budget cuts "for the good of the country."</P>
<P>As socialists, we should argue for a movement that includes anyone who opposes the war, whether or not they carry the flag. But we also have to win people to seeing that the U.S. government is not a force for good, either abroad or at home. </P>
<P>If we are going to stop the U.S. war drive, we can't avoid arguing with people that the real solution involves embracing international solidarity with all oppressed peoples rather than the Stars and Stripes.</P>
<B><P>Mitch Lewis,</B> Cambridge, Mass.</P>

