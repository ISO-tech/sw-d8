<P><font face="Times New Roman, Times, serif" size="4"><b>Racist relic of the Senate turns 100</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</B></I></P>
<P>Amid much fanfare on Capitol Hill, retiring Sen. Strom Thurmond (R-S.C) recently celebrated his 100th birthday.</P>
<P>His fellow politicians had nothing bad to say about the man who's based his 48-year Senate career on racist bigotry. Thurmond is "a man of iron with a heart of gold," gushed Sen. Joseph Lieberman (D-Conn.). Sen. Orrin Hatch (R-Utah) declared, "He is not only a great man, he's done great things in his life."</P>
<P>Oh really? Like what? Like the fact that Thurmond once ran for president promising to end "social intermingling of the races"? Or perhaps that Thurmond authored the "Southern Manifesto" that urged Southerners to disobey the 1954 <I>Brown v. Board of Education</I> Supreme Court ruling in favor of school desegregation?</P>
<P>Or possibly it was the fact that Thurmond holds the record for the longest filibuster in history--24 hours and 18 minutes--against a 1957 civil rights bill? "There are not enough troops in the Army to force Southern people to admit the Negroes into our theaters, swimming pools and homes," the bigot once commented.</P>
<P>In addition to being a first-class racist, Thurmond is a notorious sexist and lecher as well. Last year, for example, while rolling down the hall in his wheelchair, he reportedly pointed to his lap and asked a female congressional staffer: "Want a ride?"</P>
<P>Now, to honor this decrepit geezer, the Air Force is naming its 100th C-17 cargo plane the "Spirit of Strom Thurmond"--and South Carolina Gov. Jim Hodges declared "Strom Thurmond Day" in South Carolina "to reflect on the many blessings he has bestowed upon our state throughout his life."</P>
<P>The only reason to celebrate anything to do with Thurmond will be to bid him "good riddance" on the day that he draws his last bigoted breath.</P>
<B><P>Nicole Colson,</B> Chicago</P>

