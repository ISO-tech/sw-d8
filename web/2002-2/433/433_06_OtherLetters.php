<p><a name="Bush"></a><font face="Times New Roman, Times, serif" size="4"><b>A small black eye for George W. Bush</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</B></I></P>
<P>Democratic Senator Mary Landrieu's victory in the Louisiana senate runoff against George W. Bush-drone Suzanne Terrell is in no way a victory for ordinary working people. Landrieu voted with Bush 85 percent of the time during the last term and is a big money, political brat--the millionaire daughter of New Orleans Mayor Moon Landrieu. </P>
<P>Her victory, though, is a sweet and small black eye to Dubya at a time when every pundit in the wake of the November elections is speaking of his "popularity" in hushed tones. And Bush has been smirking more than ever.</P>
<P>Bush and his Republican plutocrat cronies pumped millions of dollars into the Lousiana runoff election, with Bush making many public appearances and giving Terrell his "private national donors list." In anticipation of another GOP win, Bush aides publicly dubbed the runoff "operation icing on the cake."</P>
<P>Terrell looked like she was going to win heading into the election, but the tide changed when the latest economic indicators put unemployment at 6 percent, and Landrieu made the economic future of the Louisiana sugar industry a defining issue--striking an anti-Mexico, anti-NAFTA stance with regards to one of Bush's pet projects. </P>
<P>Make no mistake, Landrieu is as craven as they come and brings new meaning to the phrase "Republican-lite." But Bush didn't get his "icing on the cake," he only got a big pie to the face. And that is a great reminder that there is a reservoir of anger against him that is only beginning to be tapped. </P>
<B><P>Dave Zirin,</B> Washington, D.C.</P>

