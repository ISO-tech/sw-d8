<P><font face="Times New Roman, Times, serif" size="5"><b>Pentagon lie machine</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 13, 2002 | Page 5</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>IN WAR, the first casualty is truth. And Donald Rumsfeld doesn't care who knows it.</P>
<P>Last year, the Pentagon announced the creation of the Office of Strategic Influence to develop, as the <I>New York Times</I> put it, "plans to provide news items, possibly even false ones, to foreign media organizations."</P>
<P>After a public furor, Rumsfeld backtracked, and the Pentagon pulled the plug on its lie machine. But at a press briefing on November 18, Rumsfeld told reporters that the Pentagon certainly hasn't given up on its work. </P>
<P>"Then there was the Office of Strategic Influence," Rumsfeld bragged. "You may recall that. And 'oh my goodness gracious, isn't that terrible, Henny Penny, the sky is going to fall.' I went down that next day and said, 'Fine&#133;[y]ou can have the name, but I'm going to keep doing every single thing that needs to be done, and I have." </P>
<P>Among other things, what Rumsfeld and the Pentagon have "done" includes having an entire division of the Air Force transfer its bomber and fighter aircraft to other commands--so that it can focus exclusively on worldwide "information attacks."</P>

