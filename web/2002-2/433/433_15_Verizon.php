<P><font face="Times New Roman, Times, serif" size="4">CWA workers at Verizon debate striking to stop layoffs</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"Not one member"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By CWA shop stewards</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 13, 2002 | Page 15</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Rank-and-file job action at Verizon has delayed the planned layoff of some 3,400 members of the Communications Workers of America (CWA). The delay is the result of a work-to-rule campaign carried out by rank-and-file workers across the city, which has forced management to push back the date of layoffs by one week to December 19.</P>
<P>Verizon is moving to break the power of the union with layoffs that would reduce their workforce in New York state by 25 percent for the year. There have never been layoffs at the telephone company in New York--and there's no basis for them today.</P>
<P>Having made $4.4 billion last quarter before paying for some workers' early retirement, Verizon is now crying poverty to the union. Management claims that the crisis in the telecommunications industry and the general economic downturn in New York City after September 11 are contractually sufficient justification for layoffs.</P>
<P>Verizon is dragging its feet in arbitration hearings. But when the CWA sought an injunction to stop layoffs before a decision had been reached, the courts ruled against the union. The company has legally cornered the union.</P>
<P>With a no-strike clause in the contract and no unfair labor practice charges to justify a strike, the CWA leadership has balked at the idea of walking out to stop the layoffs. The union could incur huge fines for an illegal strike. Yet these layoffs and the thousands that are sure to follow would signal the demise of the CWA in its core industry.</P>
<P>The stakes are huge. Just days after thousands of Verizon workers rallied against layoffs at Ground Zero, CWA local leaders held informational meetings at CWA union locals that turned ugly as low-seniority members railed at local leaders for telling them how to apply for unemployment.</P>
<P>"I thought you said, 'Not one member?'" shouted a group of technicians during a meeting in Brooklyn. The leadership was forced to say that they haven't ruled out a strike, but that all the locals in New York must act together.</P>
<P>The pressure has forced an emergency meeting of local CWA presidents in New York state where, local union leaders promise, the possibility of a strike will be raised. </P>
<P>While the CWA publicity campaign and rallies have highlighted the issue and helped mobilize the union, it was rank-and-file action on the job that forced our leaders to move towards militant actions.</P>
<P>We need to keep up that pressure. A strike authorization vote and strike preparations are a must. Picket captains should be elected and trained in each work unit. Work-to-rule campaigns could be better coordinated.</P>
<P>A solidarity campaign with Transport Workers Union Local 100, whose members have authorized a strike if their contract negotiations fail, could unite two of New York's largest and most powerful unions to fight concessions--and the CWA and other unions have endorsed the December 16 rally against budget cuts at City Hall. Every union member in New York should attend to show the strength of the labor movement when united.</P>

