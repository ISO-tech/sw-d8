<P><font face="Times New Roman, Times, serif" size="4">We shouldn't defend their right to spew hate</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Stand up to the bigots</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 13, 2002 | Page 6</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>When liberals rush to protect the free speech and First Amendment rights of homophobic bigots like hate preacher Fred Phelps, the Ku Klux Klan or anti-choice zealots, I have to disagree. </P>
<P>While being gay and serving in the Marine Corps under "don't ask, don't tell," Clinton's classic betrayal of the gay community, I remember the brutal murder of gay college student Matthew Shepherd on October 6, 1998, vividly. </P>
<P>At the time, I was stationed in San Angelo, Texas. That entire week my instructors, two staff non-commissioned officers, began class daily with cruel anti-gay jokes. They defended their own "right to free speech" to tell homophobic jokes during class and then continued to defend hate preacher Fred Phelps' right to protest Matthew's funeral with signs that read "Fags burn in hell." </P>
<P>The same freedom of speech they so passionately proclaimed for themselves and Phelps was the very freedom of speech being denied to me for being gay in the military. It was the same freedom of speech denied to Private First Class Barry Winchell, who was brutally murdered for being gay, and the same freedom of speech denied to Allen Schindler, a sailor who was similarly murdered on a Navy ship. </P>
<P>It was the same freedom of speech denied to my friend Cpl. Smith, who was bashed outside a local gay bar in San Angelo--and who the military prosecuted for being gay, rather than pursue his attackers. </P>
<P>After the teachers' defense of "free speech," one corporal said, "That fag deserved to die." The entire class was silent. No one defended Matthew Shepherd. The corporal's comment was the logical conclusion of what the two instructors had said.</P>
<P>When liberals defend freedom of speech for homophobic bigots, I think we should take a different approach. We should instead build the largest unity between queers and allies to unite and fight homophobia as possible. And when racists like the Klan show their ugly face, we should build the largest unity between people of color and whites to fight immigrant bashing and racism. </P>
<P>And when George W. Bush increases the racist scapegoating of Muslims and Arabs in association with his war on terrorism, we should build the broadest antiwar movement to stop the war--and not defend the right wing's so-called "freedom" to hate. </P>
<B><P>Martin Smith, </B>USMC veteran, Santa Cruz, Calif.</P>

