<font face="Times New Roman, Times, serif" size="5"><b>Making it safe for drug profits</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 13, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>TUCKED AWAY in the Homeland Security Act that George W. Bush recently signed into law was a provision that had little to do with protecting the "homeland"--and everything to do with protecting the profits of the pharmaceutical giant Eli Lilly.</P>
<P>Thousands of parents believe that mercury in thimerosal--a preservative used in vaccines and developed by Lilly--is responsible for causing autism and other neurological disorders in their children. But thanks to a provision in the Homeland Security Act, thousands of pending cases against the drug giant may be dismissed. The Lilly rider extends a rule that protects vaccine manufacturers from direct lawsuits, so that now the exemption applies to manufacturers of ingredients like thimerosal.</P>
<P>No politician has copped to aiding Lilly, but it's not hard to see why the company might feel entitled to a favor. In 2002, the company gave more money to political candidates--$1.6 million--than any other pharmaceutical company, with 79 percent of it going to Republicans, according to the Center for Responsive Politics.</P>
<P>The first president Bush sat on the Lilly board in the 1970s. White House budget director Mitchell Daniels Jr. is a former Lilly executive, and company chair and chief executive Sidney Taurel was appointed in June by Bush to serve on a presidential council to advise on domestic security issues.</P>
<P>"To me, it looks like payback for the fact that the industry spent millions bankrolling Republican campaigns," Andrew Water, a Dallas lawyer, told the <I>Washington Post</I>.</P>

