<p><font face="Arial, Helvetica, sans-serif" size="2">December 13, 2002 | Pages 4 and 5</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>"IT IS not a war they are starting, it's a slaughter. It will be a catastrophe." That's how Vincent Hubin, director of Premiere Urgence, the largest foreign aid agency operating in Iraq, described the Bush gang's drive for a new war on Iraq.</P>
<P>But this will be only the latest stage in more than a decade of U.S. warfare--military and economic--against the people of Iraq. Here, ANTHONY ARNOVE--editor of the book <I>Iraq Under Siege, </I>now republished in an updated edition--describes the last Gulf War and its aftermath.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>


<P><A HREF="433_04_Slaughter.shtml"><font face="Times New Roman, Times, serif" size="4"><b>It was a one-sided slaughter</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">The 1991 Gulf War against Iraq wasn't really a war at all. It was a one-sided slaughter in a country that was no match for the massive armed might of the U.S. military and its many allies.</P>

<P><A HREF="433_05_EndlessWAr.shtml"><font face="Times New Roman, Times, serif" size="4"><b>The war that never ended</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3"></font>The Gulf War left Iraq shattered. But that didn't stop the U.S. from maintaining a strict economic embargo that had been imposed by the UN following Iraq's invasion of Kuwait.</P>

<P><A HREF="433_05_Justifications.shtml"><font face="Times New Roman, Times, serif" size="4"><b>The lies they tell to justify war</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">The U.S. government never says that it's going to war for oil, profit or power. No, it only goes to war for "democracy," to "liberate" countries, to "defend human rights" or to "oppose aggression."</P>

<P><A HREF="433_04_PostSaddam.shtml"><font face="Times New Roman, Times, serif" size="4"><b>The killers they want to rule Iraq</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">Who will the U.S. government find to replace Saddam Hussein if a new assault can drive him out? Unfortunately for Washington, one of their top choices to replace Saddam might be busy when the time comes--serving a prison sentence for war crimes.</P>

