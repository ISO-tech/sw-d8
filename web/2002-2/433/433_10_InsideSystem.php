<P><font face="Times New Roman, Times, serif" size="4"><b>The Loveboat goes to Grenada</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THINK OF it as a special "U.S. imperialism" episode of the old TV show <I>The Loveboat</I>. Unfortunately, this new skipper isn't as smooth.</P>
<P>Oliver North--the former Marine lieutenant colonel at the center of the Iran-contra scandal--and his Freedom Alliance foundation are sponsoring a Caribbean cruise to honor the 20th anniversary of the invasion of the tiny island of Grenada. </P>
<P>North and a group of supporters--among them National Rifle Association nutcase Wayne LaPierre and Republican Rep. Dana Rohrabacher--will board a luxury liner and sail from Puerto Rico on March 1 of next year. "The idea of a 20th anniversary cruise seemed kind of appropriate," North said.</P>
<P>The invasion of Grenada was seen by most people for what it was--an exercise orchestrated by the U.S. anti-Castro lobby to demonstrate American power to police its "backyard." But North defends the invasion and hopes the cruise will pay proper tribute to the soldiers who risked their lives to "reverse the Communist takeover." </P>
<P>--Associated Press, November 22, 2002</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Do they have to ask?</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WHEN FRAN&Ccedil;OISE Ducros, communications director to Canadian Prime Minister Jean Chr&eacute;tien, recently called Bush a "moron" the sparks began to fly. Chr&eacute;tien defended Bush, saying "Bush is not a moron at all; he's a friend."</P>
<P>But experts on stupidity recently suggested that Ducros may be correct. "Technically, a moron is someone who is stupid but looks normal," said Albert Nerenberg, a Toronto-based film director who is completing a television documentary titled <I>Stupidity</I>. "Much has been said recently about Bush arriving at a point where he looks presidential," said Nerenberg. "What's intriguing about morons is that they can pass as just about anyone, but inside, they're still morons."</P>
<P>But Pat Buchanan, thinks the moron discussion in Canada is proof that Canadians are "the spoiled brats of the new world order."</P>
<P>--Canada Newswire, November 22, 2002</P>

