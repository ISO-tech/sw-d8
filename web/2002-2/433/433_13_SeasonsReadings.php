<P><font face="Times New Roman, Times, serif" size="5"><b>Season's readings</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 13, 2002 | Page 13</font></p>

<font face="Times New Roman, Times, serif" size="3"><I><P>Socialist Worker</I>'s columnists give their recommendations for holiday book ideas.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>


<P><font face="Times New Roman, Times, serif" size="4"><b>"Reading Between the Lines"</b></font><font face="Times New Roman, Times, serif" size="2"><b>  By Lance Selfa</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE HOLIDAYS are supposed to be a time for "peace on earth," but we know this holiday season will be filled with the drumbeats of war. So my holiday gift suggestions are for the antiwar activist on your list.</P>
<P>There are a lot of good books on September 11 and the "war on terrorism." But I'd like to suggest two: Rahul Mahajan's <I>The New Crusade</I> and Gabriel Kolko's <I>Another Century of War?</I> Mahajan shreds the myths and lies behind Bush's war. Historian Kolko shows how Washington's current predicament results from a longstanding foreign policy that "has undermined, not strengthened, the safety of the American people and left a world more dangerous than ever."</P>
<P>For the background to the current crisis in Iraq, you can't go wrong with the new edition of South End Press's <I>Iraq Under Siege,</I> an essential source for refuting every lie the U.S. is using to justify the impending war. Geoff Simon's books, <I>From Sumer to Saddam</I> and <I>The Scourging of Iraq</I> provide essential background--especially in showing just how cozy with the U.S. Saddam Hussein was at one time.</P>
<P>Finally, Gilles Kepel's <I>Jihad </I>is a rare book about Islamist political movements by someone who actually knows something. You can learn more about political Islam from one chapter of this book than from days of television "special reports."</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>"Which Side Are You On?"</b></font><font face="Times New Roman, Times, serif" size="2"><b>  By Sharon Smith</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>FOR THOSE seeking fiction to read over the holidays, I have two recommendations--both offering social and political insights, alongside gripping human drama.</P>
<P>&Eacute;mile Zola's <I>Germinal,</I> first published in 1885, gives life to Karl Marx's comment that capitalism was born "dripping from every pore" with sweat and blood. This novel tells the story of a mining family in 19th-century France, contrasting their wretched and overcrowded living conditions and constant hunger with the opulence and sheer gluttony of the families of the mine owners.</P>
<P>The book also tells the story of the miners' struggle--culminating in a bitter strike--to fight collectively for better pay and working conditions. Finally, it's a love story about two young workers trying desperately to live with meaning and dignity, with heart-wrenching consequences.</P>
<P>Sara Paretsky's <I>Hard Time</I> is a contemporary novel--a murder mystery from the well-known series featuring the hard-nosed female detective V.I. Warshawski. Warshawski is the daughter of a Chicago cop, but has neither love nor respect for the "boys in blue." And often, she finds herself on the other side of the law.</P>
<P>In this book--the best of the series--Warshawski ends up on the inside of a women's prison, and exposes not only the conditions suffered by prisoners, but the use of sweatshop labor that earns lucrative profits for the businesses that exploit prison workers.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>"The Meaning of Marxism"</b></font><font face="Times New Roman, Times, serif" size="2"><b>  By Paul D'Amato</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>POLITICS IS at a premium in today's turbulent world. So, I'm recommending Trotsky's <I>My Life</I>--ask for it in your Christmas stocking or buy it for someone else.</P>
<P>Written in 1929 after Trotsky had been exiled by the Stalinist bureaucracy, <I>My Life</I> is not some quiet, reflective memoir, but a passionate defense of his life's work (and of the real aims of the Russian Revolution) against the distortions of the Stalinist epigones. It reads like a good novel and it illuminates the debates and struggles of the socialist movement in the most revolutionary period in history.</P>
<P>If you're feeling ambitious, read as a companion to Trotsky's autobiography Duncan Hallas' <I>Trotsky's Marxism</I>. In a brief 120 pages, Hallas manages, as no other author can, to explain, in terse but expressive prose, all of Trotsky's most important contributions to Marxism.</P>
<P>And since we will be at war very soon, take this time to get some background. Read Sydney Lens' <I>The Forging of the American Empire </I>and find out what they didn't tell us in high school about American imperialism.</P>

