<P><font face="Times New Roman, Times, serif" size="4">Countdown to attack on Iraq</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Stop Bush's war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 13, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ONE EXCUSE, no matter how small or how fabricated. That's all that the Bush administration needs to launch their one-sided slaughter in Iraq.</P>
<P>Even before anyone had a chance to read the 12,000 pages of documentation on its weapons programs that Iraq submitted to the United Nations (UN), the Bush gang was already claiming that Iraq had "omitted" information from its report and was therefore "in breach" of the recent UN resolution. </P>
<P>Of course, it's not like the Bush administration could point to any specific evidence. So they're perfectly content to make up their so-called "evidence" as they go along. "It's like the judge said about pornography," Deputy Defense Secretary Paul Wolfowitz said on a recent European trip when he was asked by NATO ambassadors what it would take to prove that Iraq has failed to dismantle its weapons programs. "I can't define it, but I will know it when I see it." </P>
<P>Of course, the Bush gang will be able to "see" some cause for war--inspections or not. And leading Democrats like Sens. Bob Graham (D-Fla.) and Joseph Lieberman (D-Conn.) are enthusiastically backing Bush. "We are in possession of what I think to be compelling evidence that Saddam Hussein has, and has had for a number of years, a developing capacity for the production and storage of weapons of mass destruction," said Graham, who--without a shred of proof--claimed that Saddam Hussein was preparing to launch terrorist attacks against the U.S. in the event of an invasion.</P>
<P>It's not surprising that the Democrats would jump on board the war bandwagon. After all, killing Iraqis--through the first Gulf War and then devastating economic sanctions in the Clinton years--has been bipartisan politics in Washington for more than a decade.</P>
<P>And this war drive won't be stopped by UN inspections. As conservative pundit Bob Novak said in a rare moment of candor on NBC's <I>Meet the Press, </I>the Bush administration "never wanted to have this inspection regime because what this is really about is change of regime in Iraq and change of the political outlines in the Middle East&#133;Now, it's very difficult to find the proof of a nuclear development, to find that kind of evidence, and so it behooves--in order to make war--to say that they are hiding things."</P>
<P>Washington is driving towards war every day. We can't sit back and wait for the troops to invade and the bombs to begin falling. Antiwar activists have to seize every opportunity to come together now--before an invasion happens--and build the movement to stop Washington's war.</P>

