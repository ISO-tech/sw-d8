<P><font face="Times New Roman, Times, serif" size="5"><b>The lies they tell to justify war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 13, 2002 | Page 5</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. government never says that it's going to war for oil, profit or power. No, it only goes to war for "democracy," to "liberate" countries, to "defend human rights" or to "oppose aggression."</P>
<P>When Iraq invaded Kuwait on August 2, 1990, Washington's reaction was outrage. "A line has been drawn in the sand," George Bush Sr. declared. "If history teaches us anything, it is that we must resist aggression or it will destroy us." British Prime Minister Margaret Thatcher, Bush's main ally, added, "If we let Iraq succeed, no small country can ever feel safe again. The law of the jungle takes over."</P>
<P>But Bush and Thatcher took a different view of Iraq's aggression prior to August 2. Both countries armed and backed Iraq and supported Saddam Hussein when he was a faithful ally and served their strategic interests in the oil-rich Middle East.</P>
<P>The U.S. supported Iraq during its brutal war with Iran in the 1980s--at the cost of 1 million lives. Washington increased its aid to Iraq after the government used poison gas against Iranian troops--and then against the minority Kurdish population inside Iraq--killing thousands.</P>
<P>When U.S. ambassador to Iraq April Glaspie met Saddam in Baghdad on July 25, 1990, just one week before the invasion, "she said President Bush wanted better relations with Saddam and that the United States had no opinion on Saddam's dispute over borders with Kuwait," the <I>Washington Post</I> reported.</P>
<P>Whether or not this was meant as a trap, it's clear that the Iraqi president felt emboldened by support from Washington and thought he could get away with an attack on Kuwait. After all, the U.S. itself has just invaded Panama and engineered a "regime change," taking out its former ally Manuel Noriega and killing thousands of Panamanians--with no outcry about "the law of the jungle" or "resisting aggression."</P>
<P>It was also sheer hypocrisy for U.S. officials to talk about protecting the border with Kuwait--which was an artificial creation of British imperialists who carved up the two countries in the 1920s to better control the region's oil. Kuwait was--and is--a fiefdom for a tiny privileged elite allied with the West. But Bush proudly called the country a "friend."</P>
<P>Then he and his cronies--many of whom are back on the scene in new roles in the Bush Jr. administration--launched a relentless PR campaign to force a war with Iraq. The Pentagon announced in September 1990 that Iraq was massing hundreds of thousands of troops of its border with Saudi Arabia. But when the <I>St. Petersburg Times</I> in Florida decided to look for the evidence and bought commercial satellite images of the area, they found nothing. "That [Iraqi buildup] was the whole justification for Bush sending troops in there, and it just didn't exist," says Jean Heller, the reporter who broke the story.</P>
<P>Then the U.S. brought a 15-year-old Kuwaiti "refugee" named Nayirah to testify before Congress about how she had witnessed Iraqi troops steal incubators from a hospital, leaving 312 babies "on the cold floor to die." When the Senate voted to give support Bush Sr.'s war, by a margin of only five votes, seven senators recounted Nayirah's story in justifying their "yes" vote.</P>
<P>But the story was a hoax. Nayirah's false testimony was part of a $10 million Kuwait government propaganda campaign managed by the PR firm Hill and Knowlton. Nayirah was not a hospital volunteer, but the daughter of the Kuwaiti ambassador to Washington.</P>
<P>"We didn't know it wasn't true at the time," claims Brent Scowcroft, Bush's national security adviser. But, he admitted, "It was useful in mobilizing public opinion." </P>

