<P><font face="Times New Roman, Times, serif" size="5"><b>Media "watchdogs" kept on a short leash</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 13, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH administration is placing further restrictions on the already strict limits of what the media are allowed to see in U.S. wars.</P>
<P>Arthur Kent, who covered the 1991 Gulf War for NBC, predicted that in a new war with Iraq, Pentagon "attempts to muzzle us&#133;are going to be unprecedented." And that's saying something--given the drastic effort by George Bush Sr.'s administration to limit press coverage during the first Gulf War.</P>
<P>When the air war began in January 1991, the media were fed selected footage of U.S. bombing missions--"most of it downright misleading," according to the <I>San Francisco Chronicle</I>. Briefings by Gen. Norman Schwarzkopf and other military officers featured video images of laser-guided missiles and bombs hitting perfectly on target.</P>
<P>The Pentagon managed to not show a single picture or video of anyone being killed--even after tens of thousands of surrendering Iraqi troops were slaughtered by U.S. forces as they retreated from Kuwait on what became known as the "Highway of Death." </P>
<P>Dick Cheney later bragged that Desert Storm was the "best-covered war ever&#133;The American people saw up close with their own eyes through the magic of television what the U.S. military was capable of doing."</P>
<P>The opposite is true. Cheney and friends went to enormous lengths to make sure people in the U.S. didn't see the horror that was inflicted in their name.</P>

