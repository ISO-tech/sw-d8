<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 13, 2002 | Page 15</font></p>


<P><font face="Times New Roman, Times, serif" size="4"><b>Boeing</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Darrin Hoop</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SEATTLE--The 24,000 members of the Society of Professional Engineering Employees in Aerospace (SPEEA) passed by an 88 percent margin a three-year contract with Boeing Co.</P>
<P>The contract has two separate parts, one for professionals or engineers and one for the technical workers. Both the engineers and technical workers won higher minimum monthly pension pay, a 6 percent bonus after ratification and a new Boeing-paid short-term disability plan. </P>
<P>However, the wage increases are less than the last contract. Boeing is offering a 4 percent wage increase for both units, but only 1.5 percent of this is guaranteed each year to engineers and only 2 percent is guaranteed to technical workers. The rest is up to the managers' discretion.</P>
<P>The union also agreed to an increase in members' health care premiums, and the contract contains none of the job-security language that the union had wanted--such as a lay-off moratorium or early retirement options.</P>
<P>"Boeing is salivating at the prospect of outsourcing engineering work," said a six-year Boeing engineer. Since the September 11 attacks, more than 25,000 Boeing jobs have been cut--16,000 in the Puget Sound region--and another 6,500 more are slated to be cut next year.</P>
<P>Dave Landress, chairman of SPEEA's technical negotiating team, defends the agreement. "We believe these proposals are the best we could secure in the current environment," he said.</P>
<P>But in February 2000, the members of SPEEA took part in a 40-day strike that was the longest and largest strike by white-collar union workers in U.S. history. By striking, SPEEA won higher guaranteed wages, and there were no benefit takeaways.</P>
<P>The key to the strike was solidarity. While SPEEA represented only 63 percent of engineers and technical workers, nearly 90 percent walked off the job. In addition, members of the International Association of Machinists (IAM) refused to perform the work of SPEEA members during the strike. UPS and Airborne drivers, who are members of the Teamsters, honored the picket lines. </P>
<P>Boeing workers have been pressured by the miserable state of the airline industry.  Boeing's jet production is slated to fall from 527 jets in 2001 to 275 in 2003. "The people have had their head in the sand," one Boeing engineer said. "The inability to get job security language is because people aren't facing up to it. They're not thinking, 'In three years I might not have a job.' There are a few engineers who think about it, but we aren't organized enough." </P>
<P>In the future, it will take a replay of the solidarity shown in the 2000 strike to win better contracts.</P>

