<P><font face="Times New Roman, Times, serif" size="4"><b>AFSCME Local 768</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Thomas Barton</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--As a bitterly contested run-off election nears for health care workers in AFSCME Local 768, Mayor Michael Bloomberg started firing city workers this week. He's also demanding that city employee unions give up benefits won decades ago. </P>
<P>The $600 million in givebacks include upping the workweek from 35 to 40 hours, cuts in sick and vacation time and higher paycheck deductions for pensions. And he wants city workers to start paying for their own health care. </P>
<P>Layoffs have already started--with more promised if the new AFSCME contract, covering 125,000 city workers, doesn't give Bloomberg what he wants. At the Department of Education alone, 380 workers will lose their jobs January 2. The AFSCME contract expired June 30--and along with it the "no-layoff" protection in the old contract.</P>
<P>The election fight inside Local 768, with its 4,000 members, reflects the broader crisis. Local 768 is one of the last big District Council 37 unions still run by the old guard formerly loyal to the disgraced and ousted union chief Stanley Hill--but a Local 768 reform slate headed by Fitz Reid has come out swinging. They pledge to fight layoffs or givebacks and to campaign against the Taylor laws that forbid city workers to strike.</P>
<P>Unlike the "reformers" who stuffed their own pockets after Hill was kicked out, the Reid slate also pledge that they won't take one cent more as union officers than they earned on the job. And if the workers that they represent don't get a raise, neither will they. They also promise to resign if a majority votes to recall them.</P>
<P>In the December 2 election, the old guard got a minority of the votes, but two splinter candidates took enough votes to force a run-off between Reid and the old guard.</P>
<P>In the past 25 years, the corporate income tax as a share of New York State's tax revenues has been cut 60 percent. AFSCME leaders publicly admit that in good times the bosses in city government cut taxes on corporations and the rich and in bad times lay off workers and cut benefits. But so far they refuse to organize to fight--instead babbling about how they and the city bosses are "partners."</P>
<P>Of course, AFSCME union leaders don't have to worry about cuts to their pay, which exceeds $200,000 a year for some. If sacrifice is necessary, it's time they get their taste of it. We've had our share already.</P>

