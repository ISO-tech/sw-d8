<P><font face="Times New Roman, Times, serif" size="4">Chomsky film shreds the September 11th lies</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Terror made in the USA</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Brian Jones</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 13, 2002 | Page 13</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>DOCUMENTARY: <I>Power and Terror: Noam Chomsky in Our Times,</B></I> a film by John Junkerman, produced by Yamagami Tetsujiro.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>COMPRISED OF footage from recent speeches and interviews, the documentary <I>Power and Terror</I> is Noam Chomsky doing what he does best: exposing the hypocrisy of the world's richest and most powerful countries.</P>
<P>Disheartened by the wave of patriotism following the September 11 attacks, filmmaker John Junkerman set out to give voice to an alternative perspective. And who better to turn to than Chomsky?</P>
<P>Famous for his encyclopedic knowledge of world politics, Chomsky is one of the most influential dissident intellectuals in the world. And at the age of 73, he maintains a demanding speaking schedule.</P>
<P>In the film, we find him at a lectern. He adjusts the microphone and proceeds for next hour--sometimes humorous, sometimes sarcastic, but always mild-mannered and matter-of-fact--to systematically dismantle what the American public has been told about the "war on terror."</P>
<P>The atrocities committed on September 11, he admits, are in fact historic. "Unfortunately, not because of the scale, but because of who the victims were." After all, if terrorism is defined as the use of violence against civilians for political ends, then the U.S. government is the greatest terrorist of them all.</P>
<P>Chomsky recalls many of these atrocities, such as George Bush Sr.'s "Operation Just Cause," in which 3,000 Panamanian civilians were massacred in one village. Chomsky points out the hypocrisy of liberal intellectuals who supported the bombing of Afghanistan, but not of Washington, D.C., after operations like "Just Cause." "If you want to stop terrorism, stop participating in it," Chomsky argues.</P>
<P>Some of the most interesting moments in the film are those in which Chomsky responds to questions from the audience. When an audience member asks, "What is the mechanism by which the government controls the media?"</P>
<P>Chomsky turns the question around. "That's like asking how does the government make GM increase profits?" The media, he explains, is of course itself a large corporation, and shares the same interests as the rest of the rich and powerful in society--and they willingly censor themselves accordingly.</P>
<P>There are aspects of Chomsky's politics that socialists will take issue with, such as the idea that our society is "state capitalist," in which the state has replaced all forces of the market. This minor criticism aside, this film is a much-needed response to the mainstream commentary on terrorism.</P>
<P>In our society, there exists a tendency toward the destruction of humanity, Chomsky argues, but at the same time, a "tendency toward civilization"--the movements for freedom and democratic rights around the world.</P>
<P>"Which curve is going to move up faster," he tells the audience "will determine the fate of the species--that question is pretty much in the hands of people like you."</P>

