<P><font face="Times New Roman, Times, serif" size="5"><b>An anthem for the struggle against racism</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Amy Muldoon</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 13, 2002 | Page 13</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>DOCUMENTARY: <I>Strange Fruit,</I> </B>produced and directed by Joel Katz, distributed by California Newsreel.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>"BLACK BODIES swinging in the Southern breeze, strange fruit hanging from the poplar trees." These painful words as sung by Billie Holiday are from "Strange Fruit," one of the most visceral and lasting U.S. protest songs.</P>
<P>The song was one of the most controversial of the late 1930s, when lynching continued to be the brutal edge of racism for Blacks in the South. <I>Strange Fruit,</I> a 2001 documentary by Joel Katz now showing at New York's Film Forum, explores the history of the song and the political moment that gave birth to it.</P>
<P>Record companies didn't want to record "Strange Fruit," and when Columbia Records finally did, radio stations banned it. The song rose to Number 16 on the charts anyway.</P>
<P>Abel Meeropol--a Bronx school teacher and Communist Party activist--wrote "Strange Fruit." He and his wife, Anne, are also known for adopting the sons of Julius and Ethel Rosenberg, who, as part of the McCarthyite witch-hunts, were executed as "atomic spies" in 1953.</P>
<P>"If you were anti-lynching at that time, you were thought to be a Communist," civil rights activist C.T. Vivian tells filmmakers. And with good reason--as the film shows, with footage of dozens of protests and marches against racism with the Communist Party playing a prominent role.</P>
<P>The song was originally sung by Meeropol's wife, Anne, at Communist-sponsored theater events before being brought to Holiday. At first hesitant to sing it, Holiday eventually made the song a trademark number.</P>
<P>She often prefaced the song by saying it was "a tune written especially for me." But Meeropol said that wasn't the case. "I wrote 'Strange Fruit' because I hate lynching, I hate the racism that created it, and I hate the people that promote it."</P>
<P>Katz also makes clear that racism and lynching are not just stories about the 1930s. A photo montage shows protests for Amadou Diallo, the victim of racist New York cops, modern-day lynching victims James Byrd and Matthew Shepard and finishes with a post-September 11 sign that reads, "Avenge the USA. Kill a Muslim."</P>

