<P><font face="Times New Roman, Times, serif" size="4">Workers may strike</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>NYC transit heads for showdown</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Shaun Harkin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 13, 2002 | Page 16</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>Thousands of New York's subway and transit workers, members of Transport Workers Union (TWU) Local 100, voted unanimously at mass meetings in early December to authorize a strike when their contract expires December 15.</P>
<P>Billionaire Mayor Michael Bloomberg and Gov. George Pataki, both Republicans, have declared war on the 33,000 bus and subway workers covered by the contract. In response to the union's demand for a 24 percent wage increase over three years, Bloomberg wants to impose a first-year wage freeze, a 2.3 percent increase in pension payments and increased health care payments by union members. Effectively, this is a pay cut.</P>
<P>However, New York's anti-labor Taylor laws prohibit public workers from striking and impose outrageously heavy fines if workers do walk out. During the last contract fight in 1999, a judge threatened to jail workers for even saying the word "strike"--and the mayor is making similar threats now.</P>
<P>Despite Bloomberg's attempt at intimidation, transit workers turned out in thousands to two mass membership meetings in Manhattan. Shooks, a maintenance worker in the Bronx, told <I>Socialist Worker,</I> "The [Transit Authority] doesn't want to negotiate; they're looking for a strike. The Republican administration is playing tough games, but the workers are prepared to do anything and everything--it's a war. The expectations are high. The main issues are job security and wages keeping up with inflation. I'm not beyond striking."</P>
<P>Other TWU members agreed. "They think we're afraid of the Taylor law," said another Local 100 member, Charles Rogers. "I'm willing to take a hit, to do better in the long run. Otherwise they'll run us over. You've got to stand up to get what you need. You can't allow them just to impose anything on you. They don't want to give us anything. They are pushing us into a corner. It looks like we're going to have to strike. I hope not. It's hard on the city, but it might come to that."</P>
<P>Another worker, Ram Singh, said, "I hate to say this, I have a wife and kids, but I'm willing to go to jail if I have to. We have to turn things around this time."</P>
<P>TWU Local 100 President Roger Toussaint was elected after criticizing the old-guard union leadership for failing to stand up to management threats--and he hasn't ruled out a strike. But Toussaint has also backed away from his earlier militant positions--and he's under serious pressure from city officials to accept a concessionary contract.</P>
<P>In fact, labor leaders in New York have so far failed to fight Bloomberg's budget cuts even though his popularity has plummeted in recent weeks. This is why it's absolutely crucial for rank-and-file worker to be organized in the TWU and other municipal unions.  Without rank-and-file organization, sentiment for a strike won't turn into action.</P>
<P>A network of transit workers, called Rank and File Advocate, distributed a leaflet at the mass meetings arguing the union should prepare to strike and begin electing picket captains in each workplace and set up a strike headquarters immediately. These are the kinds of activities that can prepare the rank and file for a walkout--but the network needs to grow much more.</P>
<P>If transit workers strike, it will build the confidence of workers all over the country in fighting the idea that we need to sacrifice for their profits.</P>
<P>TWU Local 100 initiated a demonstration December 16 against the budget cuts. If transit workers strike when their contract expires, it would be a fantastic opportunity to galvanize transit members and their supporters to win the kind of contract they deserve--and give a big boost to the entire labor movement.</P>

