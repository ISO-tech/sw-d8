<P><font face="Times New Roman, Times, serif" size="4">ILWU rank and filers speak out against proposed contract</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Why we're voting no</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 13, 2002 | Page 15</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>DELEGATES REPRESENTING some 10,500 West Coast dockworkers in the International Longshore and Warehouse Union (ILWU) met in San Francisco the week of December 9 to discuss last month's tentative agreement in their struggle for a contract with the Pacific Maritime Association.</P>
<P>JACK MULCAHY is a 27-year member of ILWU Local 8 in Portland, Ore., and a former business agent. MARK DOWNS, a member of ILWU Local 19 in Seattle, started working on the waterfront in 1963 and has been a member of his local's executive board several times. They spoke to <I>Socialist Worker</I>'s LEE SUSTAR about the proposed contract, the employers' lockout, and George W. Bush's intervention under the Taft-Hartley Act to impose an 80-day cooling-off period that banned work stoppages and slowdowns.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<B><P>This contract was negotiated under Taft-Hartley--the "slave labor act." How does that play out in your understanding of how this contract was negotiated?</P>
<P>Jack: </B>We didn't negotiate the contract. It was forced on the union, basically. The employers knew early in the negotiations, if it came down to a strike or a lockout, they would get the Taft-Hartley evoked. That was their plan from the outset. I don't think anybody would deny that.</P>
<B><P>The union never held a strike authorization, and the contract was extended day by day. Why are those things important?</P>
<P>Jack:</B> The ultimate weapon is the strike but you also are entitled to work safe and work slow if you want to put that kind of pressure on the employer. But we lost that advantage by continuing the contract on a day-by-day basis.</P>
<B><P>When the lockout happened what was the reaction in the union?</P>
<P>Mark: </B>Anger. It felt like we had been assaulted. We were making lots of money for them and then we are on the street with burn barrels, making us look like villains in the press. It's changed the relationships forever.</P>
<B><P>How did the bosses try to divide workers?</P>
<P>Jack:</B> The employers claim they don't have the money to disburse throughout contract to the rank and file, so they throw a lot of money to one sector of the workforce. It's a simple tactic, you've heard it a million times, divide and conquer. The union should just demand to disperse the money the way we want it dispersed.</P>
<B><P>What kind of impact will this have in the ILWU? </P>
<P>Jack: </B>It's hard to say. It's a contract that's made to divide and weaken the union. It's a six-year contract. There's no open review on the technology. No open review on the wages. I think that there is a good chance that, even if we do accept the contract, we do not have a guarantee from all the senators and congressmen of this country that they're not going to put the ILWU under the [National Railway Labor Act] or some other anti-labor legislation.</P>
<B><P>Where do you go from here?</P>
<P>Jack: </B>Well we try to rally as many people as we can to not have the decision process made strictly on a fear campaign where it's either take this contract or take whatever they give you in legislation.</P>
<P>The ILWU has never run from a fight in the past. We've managed to come out of them intact. We should take the same approach as our history has shown us. We should vote the contract down. Send the negotiators back. Tell the employers we need safeguards against the elimination of clerks and longshore jobs under the new technology. They need to disburse the money equally to the ranks.</P>
<B><P>HOW DOES this fight on the docks fit into this overall picture in terms of the U.S. labor movement today?</P>
<P>Jack:</B> With Bush's attack on labor, particularly government employees, and the Homeland Security Act (passed by Democrats also), it seems like there is a hunger in the labor movement for some leadership to be out front.</P>
<P>The ILWU has traditionally taken that role. Those of us rank and filers would like to continue that leadership role, take a hostile stand against the ship owners, against Taft-Hartley, reject both of them, go back to the negotiating table and demand we get a fair contract.</P>
<B><P>Mark:</B> Instead of orienting to a business strategy, we should restore the injury to one is an injury to all. There's been a lot of opportunities this past year to do that. Bush appeared in Portland right in the middle of this and said there would be military on the docks. People wanted to go there and protest, but the leadership called off our participation.</P>
<P>I spoke with some others on the war with Iraq and how it was an attack on the working class here, and there was overwhelming support. All these people are looking to be part of a fight for justice and against the cuts of the Democrats and Republicans.</P>

