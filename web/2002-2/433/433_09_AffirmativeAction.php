<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The bigots' attack on affirmative action</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 13, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IF YOU believe that you have been a victim of racial discrimination, don't bother appealing to the Center for Equal Opportunity--unless you happen to be white.</P>
<P>Despite its progressive-sounding name, the center is dedicated to overturning affirmative action. Affirmative action was a victory of the civil rights movement, along with school desegregation and the Voting Rights Act of 1964--which were aimed at combating institutional racism.</P>
<P>The ideological battle against affirmative action as a symbol of this victory has been raging ever since--succeeding in gutting diversity programs by virtue of role reversal, in which opponents claim the mantle of civil rights in their battle against "reverse racism" toward whites.</P>
<P>In 1978, the Supreme Court made its first landmark affirmative action decision, ruling in favor of Allan Bakke, who claimed he was a victim of "reverse discrimination" after he was rejected by the University of California at Davis Medical School.</P>
<P>At the time, the medical school set aside 16 of its 100 annual openings for non-white students. The court disregarded the fact that the medical school also set aside a certain number of places each year for the sons and daughters of wealthy (white) alumni.</P>
<P>In 1993, the Supreme Court ruled against an attempted voter redistricting in North Carolina--where there had not been a Black member of Congress since Reconstruction--that would have modestly increased Blacks' voting power. Ruling in favor of five white voters, the Court claimed the redistricting would result in "an uncomfortable resemblance to political apartheid."</P>
<P>After thousands of Black Florida voters were disenfranchised in the 2000 elections, the Supreme Court halted a ballot recount--handing Bush the election--absurdly citing the "equal protection" clause of the 14th Amendment in its decision.</P>
<P>Attorney Ted Olsen of the Center for Individual Rights argued the case on Bush's behalf. Olsen is the same attorney who in 1996 won a lawsuit against the University of Texas Law School that ended affirmative action there. Now Olsen has set his sights even higher.</P>
<P>Last week, the Supreme Court agreed to review two cases in early 2003, both against the University of Michigan, brought by the Center for Individual Rights with the goal of outlawing affirmative action altogether.</P>
<P>The lawsuits contend that three white applicants who were rejected by the University of Michigan Law School and the undergraduate program were victims of "a race-based two-track admissions system" that denied them the constitutional right to "equal protection." Apparently, some Black and Latino students with similar or lesser academic records than the three whites were admitted to the university.</P>
<P>Yet Blacks and Latinos remain grossly underrepresented at the University of Michigan. The law school is just 6.7 percent Black and 4.4 percent Latino. Among undergraduates, just 8.4 percent are Black, and only 4.7 percent are Latino. Rather than representing "preferential treatment" toward racial minorities, this seems clear evidence of continued discrimination.</P>
<P>Far from a "level playing field," as opponents of affirmative action claim, racial discrimination is pervasive throughout U.S. society--as evidenced by a recent report on racial disparities in school funding by the Education Trust. In densely-populated New York, for example, districts with the highest proportion of minority students receive more than $2,000 less per student annually than those with the highest concentration of whites.</P>
<P>And because of racism, Blacks and Latinos are disproportionately represented in the working class, with the least opportunity for advancement. According to www.inequality.org, the typical Black household possesses only 12 percent of the wealth of the typical white household; excluding housing, the figure falls to just 3 percent.</P>
<P>Affirmative action should be criticized not because it has "gone too far" but because it hasn't gone nearly far enough to combat racial discrimination in U.S. society. And affirmative action programs should be defended, while fighting to reclaim the fight against racism from the bigots who have turned it on its head.</P>

