<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Killed by the crisis in health care</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 13, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>PRESIDENT BUSH reacted to news of a sharp increase in unemployment to 6 percent the only way he always deals with bad news--looking for a scapegoat. He found two, firing Treasury Secretary Paul O'Neill and economic adviser (and former Enron adviser) Lawrence Lindsey.</P>
<P>O'Neill and Lindsey may be unemployed, but no one should feel sorry for them. After all, these millionaires are not going to face the terror that ordinary people who lose their jobs face. People like Diane McPherson of Lowell, Mass., who lost her job last year. </P>
<P>She and her husband couldn't afford to pay for the family health plan her job provided. So they decided to insure only their daughter. When Diane's unemployment benefits recently ran out, they had to drop their daughter's coverage, according to the <I>New York Times</I>. When their daughter came down with strep throat, they had to beg for treatment. "That was rather humiliating, being in the doctor's office without insurance," MacPherson said. "You become very obvious to everyone."</P>
<P>About 41 million Americans face the kinds of choices that Diane McPherson faced. Thousands more are joining the ranks of the uninsured daily, as 1.4 million did in 2001. It is a crisis that's actually costing them their lives. People like Audrey Robar of Milwaukee.</P>
<P>When she lost her job last year, she gambled that she could get along without the $300 a month payment she would need to maintain her health benefits under the federal COBRA law. But when Audrey had a heart attack, she spent precious minutes looking for COBRA papers instead of calling for an ambulance. She died before the ambulance arrived.</P>
<P>"I think the fact that she hadn't paid for COBRA very well could have cost her life," Audrey's daughter told the <I>New York Times</I>. "She deliberated over calling an ambulance at a time when every minute was urgent."</P>
<P>You'd think any self-respecting member of Congress would want to champion the needs of people like Diane and Audrey. But they're too busy doing favors for corporations instead. Congress refused to extend unemployment benefits for the nearly 1 million workers who will lose them on December 28. Meanwhile, they worked overtime for drug maker Eli Lilly, passing a provision exempting it from liability for the effects of thimerosal, a chemical some research has linked to autism in children. This provision stripping the rights of hundreds of thousands of Americans was an 11th-hour addition to legislation creating the massive Homeland Security agency.</P>
<P>But the health care crisis is getting so serious that some Democrats are making noises about it. Former Vice President Al Gore has even announced support for a government-run "single payer" health care system. While that's a welcome change of rhetoric, we should remember that the Clinton-Gore administration failed to tackle the crisis as it worsened through even the 1990s economic boom.</P>
<P>Hope for real change--for a system that makes health care a right for everyone--can only come from a movement that won't settle for less.</P>

