<P><font face="Times New Roman, Times, serif" size="4">Say no to Bush's war on Iraq</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Activists grill campus hawk</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 13, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ANTIWAR ACTIVISTS at the New School University in New York City are turning up the heat on pro-war university president and former senator Bob Kerrey.</P>
<P>On December 4, 1,000 people came to grill Kerrey on his pro-war stance at a public forum organized by antiwar activists. The forum was the result of a sit-in at Kerrey's office last month, in which students demanded he defend his views publicly.</P>
<P>After statements by Kerrey, Mustapha Tlili of the World Policy Institute, antiwar students Colin Walsh and Anne Kirkham and the general audience took on Kerrey in an hour-long open mike session.</P>
<P>When asked whether he agreed with former Secretary of State Madeleine Albright that the deaths of 5,000 Iraqi children per month due to sanctions are "worth the price," Kerrey replied, "Yes, it is worth the price" to boos from the shocked audience. About 40 students held up signs demanding that Kerrey resign either from the pro-war Committee for the Liberation of Iraq or from the presidency of the New School.</P>
<P>Kerrey left the stage pale and shaking. Now Kerrey is planning for his own "teach-in" on the war. Activists have vowed to keep up the pressure.</P>
<P>Chanting "Out of the classroom and into the streets!" more than 600 Seattle high school and college students staged a walkout against the war on December 5. Organized by the City Wide Student Antiwar Coalition, students from three colleges and several high schools marched to oppose Bush's war plans.</P>
<P>"This is not our war!" said march organizer Dustin Washington. "This is a war for Bush and his buddies and the oil companies." Sixty students from the University of Washington marched to Seattle Central Community College to join students from all over the area. The coalition is planning an emergency response student walkout for the day after the war starts.</P>
<P>Washington, D.C., antiwar activists joined others around the country in preparing for day of actions on December 10 to commemorate International Human Rights Day. They're planning civil disobedience at an Army recruitment center, a peace march and an evening picket outside the offices of the pro-war Committee for the Liberation of Iraq.</P>
<P>On December 7, 100 people attended a "Rock against War" benefit concert organized by antiwar activists in Burlington, Vt., to help raise funds for buses to get to the national antiwar demonstration in Washington, D.C., on January 18.</P>
<I><P>Ben Dalbey, Sarah Grey, Steve Leigh, Afsaneh Moradian and Keith Rosenthal contributed to this report.</P></I>

