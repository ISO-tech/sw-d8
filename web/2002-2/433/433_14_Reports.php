<P><font face="Times New Roman, Times, serif" size="4"><b>Another world is possible</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Steve Leigh</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SEATTLE--Some 500 people marched in downtown Seattle to oppose Bush's war on Iraq and corporate globalization on November 30. The march was a commemoration of the "Battle in Seattle"--the protest of more than 50,000 people that shut down the meeting of the World Trade Organization (WTO) in Seattle in 1999. </P>
<P>This year, the rally focussed on opposing the coming war on Iraq and continuing opposition to the WTO, International Monetary Fund and World Bank. These institutions impose poverty on people throughout the world through tax cuts for the rich, deregulation of industry, slashing of social programs and privatization of government services. </P>
<P>The marchers pointed out that Bush imposes this on the U.S. as well and enforces it abroad using the U.S. military to back up pro-corporate economic policies.</P>

