<P><font face="Times New Roman, Times, serif" size="4"><b>Stop the Nazis</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alana Smith</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LEWISTON, Maine--City officials granted a permit to white supremacist organizations planning to hold a rally here January 11.</P>
<P>Lewiston has been targeted by the National Alliance and the World Church of the Creator--both neo-Nazi groups--ever since Lewiston Mayor Larry Raymond wrote an open letter to the town's large Somali community. In his letter, Raymond "requested" that the Somali community not encourage any more immigration to the town because of the supposed financial strain. </P>
<P>City officials say that these hate groups have a constitutional right to rally. In fact, officials moved the rally from the local community center to the Lewiston Armory--a much larger venue--because hundreds of people are expected to attend.</P>
<P>The National Alliance has already begun spreading racism in Lewiston by distributing flyers that say, "Somalians: Savage People from a Savage Place" and "When American-Haters Become Americans."</P>
<P>Already, antiracist forces are organizing an opposition to this hatred. "The National Alliance is working to target tensions resulting from the lack of housing and jobs in the town onto the Somali population," reported Maine IndyMedia.</P>
<P>Only by joining together in struggle can the fight for our jobs, our homes and our health care be won. When the mayor wrote his open letter to Lewiston's Somali residents, people took to the streets in protest. Now, we have to make sure that an even larger and more diverse group of antiracists stands up to the Nazis in January.</P>

