<P><font face="Times New Roman, Times, serif" size="4">Boston janitors make headway with their strike</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"I'll go back when we win"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alpana Mehta</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>BOSTON--"&#161;Boston, escucha, estamos en la lucha!" "Listen up, Boston! We're in a struggle!" Since janitors began their strike here at the end of September, marchers have taken their struggle to clogged downtown streets--often during rush hour. Now, they're starting to get results.</P>
<P>On October 5, six contracting companies signed interim agreements with Service Employees International Union (SEIU) Local 254 to reduce the number of hours that the janitors must work to qualify for health insurance. </P>
<P>Currently, janitors need to work 29 hours per week to qualify for health benefits, and the new agreement lowers this to 27.5. This is a step forward for the nearly 11,000 janitors represented by SEIU Local 254, only 25 percent of whom currently qualify for health care. </P>
<P>But even those guarantees aren't ironclad. For example, many part-time workers in large buildings have to work extra hours to clean their areas--hours that don't count towards qualifying for health benefits. In addition, full-time janitors have no family health care options, and many can't afford to pay their own single-person insurance premiums. </P>
<P>Local 254 also wants to increase the miserably low wages of janitors--who currently earn $10 an hour on average--and to increase number of full-time positions from 1,900 to 3,400 over the life of the three-and-a-half-year contract. The union wants a pay increase of $14 an hour over the life of the contract for all janitors working within 25 miles of downtown Boston. </P>
<P>SEIU Local 254 and its supporters have stepped up the militancy on picket lines. They've organized roving demonstrations and a sit-in at Callahan Tunnel, a major throughway into downtown Boston, resulting in 25 arrests.</P>
<P>The pressure on contractors to settle the strike also increased after John Hancock Financial Services offered to pay an additional $1.5 million for contracted janitors. An average janitor in Boston makes about $39 a day. Compare that to the numbers for Unicco Service Co., the company that hires the janitors and has contracts to clean buildings throughout the Boston area. Last year, Unicco raked in $600 million in revenues and $20 million in pretax profits.</P>
<P>And Unicco CEO Steve Kletjian "earned" $1.2 million last year. Kletjian has the audacity to sneer at the union's demand for health care and better wages, calling them "red herrings." And he recently whined to a local reporter that Unicco is happy to hire more full-time janitors if they simply ask for such positions--a bald-faced lie.</P>
<P>But for Unicco, the largest contracting company in New England, this fight is about more than health care. Unicco wants to undercut the strength of the janitors' union.</P>
<P>But workers aren't about to give up without a fight. Mariano, a typical janitor in Boston, earns $154 a week after three years on the job. "I go back when we win," she told the <I>Boston Globe</I>.</P>
<P>Currently, about 2,000 of the nearly 11,000 janitors in SEIU are out on strike. While the business pressures on Maintenance Contractors of New England, the grouping of various contractors, are significant, the strength of the strike is dependent on the union's own solidarity--with all workers walking out. </P>
<P>SEIU already has strong support from other local unions. The AFL-CIO has agreed to honor the picket line, and union members from the Teamsters and the Communications Workers of America have also refused to cross picket lines.</P>
<P>Mayor Thomas Menino--who has involved himself in the strike--is posing as a friend of the union. But we can't trust Menino, who stalled the strike for weeks and now supports a court order barring street demonstrations without permits. Two years ago, Menino showed whose side he's on when he refused to negotiate with Boston firefighters in an attempt to bust their union.</P>
<P>We need to increase the pressure and spread the message--justice for janitors!</P>

