<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Why the UN resolution on Iraq is designed to lead to war</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Making an offer they can't accept</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 11, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH administration has a new twist on the old line from the <I>Godfather</I> movies: Make them an offer they can't refuse. In the case of Iraq: Make them an offer they can't possibly accept.</P>
<P>The draft resolution that the U.S. is trying to push through the United Nations (UN) Security Council requires Iraq to agree to "immediate, unconditional and unrestricted access to any and all areas [and] facilities. Any permanent member of the Security Council may request to be represented on any inspection team with the same right and protections," the resolution continues.</P>
<P>In other words, the U.S. would have free rein to snoop anywhere in Iraq, at any time. Provisions like these should give pause to antiwar activists who support UN weapons inspections as an alternative to Bush's war drive.</P>
<P>Washington has served notice that it will bribe or strong-arm anyone it needs to in order to get a UN resolution passed on its terms. And those terms will make weapons inspections a springboard to military action--literally.</P>
<P>The White House's draft resolution calls for "military backup of any inspections, and authorization for the United States or its allies to destroy any arms caches or weapons laboratories that the United Nations inspectors find," the <I>New York Times</I> reported. This "military backup" could become the core of an invasion force, according to analysts.</P>
<P>Meanwhile, inspectors would have the right to declare "no-fly/no-drive zones, exclusion zones, and/or ground- and air-transit corridors, which shall be enforced by UN security forces or by members of the Council." And looming over the bureaucratic language is the threat of all-out war--UN member states would have the right to use "all necessary means" if Iraq doesn't comply.</P>
<P>The White House doesn't care about getting weapons inspectors into Iraq. It cares about getting Iraq to say no. This is a familiar scenario. The U.S. used the same technique to move ahead with its 1999 bombing campaign against Serbia.</P>
<P>Under the Rambouillet Accords, Bill Clinton--with the backing of Washington's NATO partners--demanded that Yugoslav President Slobodan Milosevic grant complete access to Western troops, a condition that no sovereign nation's leader could agree to.</P>
<P>A senior State Department official boasted that the U.S. "had intentionally set the bar too high for the Serbs to comply&#133;They need some bombing, and that's what they're going to get." Today, Bush is playing the same cynical game in the run-up to his war on Iraq.</P>
<P>Many people were understandably outraged by Bush's swaggering speech before the UN last month--and his bald-faced attempt to intimidate the rest the world into backing a new war on Iraq. But calling for the UN to take the leading role as an alternative is no solution.</P>
<P>At the root of this call is the mistaken belief that the UN is an "impartial" body that can find a peaceful, and perhaps fairer, solution. But the U.S. regularly ignores the UN Charter and any number of UN resolutions if they come in conflict with American interests.</P>
<P>And more to the point, the U.S. government is able to call the shots at the UN on almost any issue it wants to. As the world's wealthiest nation and the only military superpower, the U.S. is powerful enough to get the other permanent members of the Security Council to go along--or at least abstain--on the issues it most cares about.</P>
<P>And when it benefits the U.S. politically, Washington uses the UN to disguise its aims. In Somalia, the UN provided U.S. troops with the cover of "peacekeepers" as they killed or wounded 10,000 Somalis during a two-year period in the early 1990s. And no one should forget that the 1991 Gulf War against Iraq was carried out under the banner of the United Nations--as was the decade of economic sanctions that followed.</P>
<P>The UN will likely give Bush cover--in the form of seeking out "weapons of mass destruction"--to accomplish his real goal: "regime change" in Iraq.</P>
<P>Antiwar activists shouldn't call for inspections instead of war. Because, in the end, they will almost certainly get both.</P>

