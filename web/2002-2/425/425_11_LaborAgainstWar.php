<P><font face="Times New Roman, Times, serif" size="5"><b>More unions pass antiwar resolutions</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Thomas Barton, trustee, AFSCME Local 768</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 11, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AS NEW York City Labor Against War (NYCLAW's) October 19 organizing conference approaches, official labor bodies are taking tough positions against Bush's war. Not stopping with mere words, they're calling for action now: "Resolved, that the Albany Central Federation of Labor endorse 'Stop-the-War' rallies including the International Day of Protest on October 26, 2002, in Washington, D.C., and other cities."</P>
<P>Giving Bush's excuses for invading Iraq the back of their hand, the resolution continues: </P>
<P>"Whereas, the Bush Administration's war drive has a domestic component threatening to turn his 'endless war' against the International Longshore and Warehouse Union (ILWU) and as an opening wedge against the entire labor movement, by threatening government intervention on the West Coast docks under the guise of 'Homeland Security,' on the side of the Pacific Maritime Association bosses and a coalition of anti-union corporate interests including Wal-Mart and the Gap therefore be it:</P>
<P>"Resolved, that the Albany Central Federation of Labor join the growing movement in opposition to any US war against Iraq, and call on the unions and AFL-CIO at all levels and Congressional representatives to publicly oppose this war&#133;"</P>
<P>The Washington State Labor Council resolution passed in August not only opposes the war, but takes on national union leaders like AFL-CIO President John Sweeney:</P>
<P>"Whereas the national AFL-CIO's uncritical support for this profit-driven war has derailed labor opposition to increase military expenditures, corporate subsidies and government spying and provided political cover for Democrats to jump on the anti-terrorism bandwagon:</P>
<P>"Resolved that the Washington State Labor Council urge the AFL-CIO and its affiliates to oppose the U.S. government's open-ended 'war on terrorism' and participate in rallies, marches and other activities&#133;"</P>
<P>The California Federation of teachers followed suit at its convention last month, going on record "as strenuously opposing the Bush administration's march toward war with Iraq." Other resolutions against the war have come from the United Electrical workers and 1199/SEIU Health and Human Services Union in New York City. Sections of the Public Services Employees Union and the Civil Services Employees unions in New York voted to support the October 6 mass rally of 25,000 in Central Park against the war.</P>
<P>The October 19 New York City Labor Against War organizing conference could play a key role in pushing labor opposition forward and building national labor unity against Bush's oil war.</P>
<P>Among new endorsements for the Conference: AFSCME Local 215, NY Taxi Workers Alliance, Transit Workers Against the War, Vietnam Veterans Against the War, Albany Labor for Peace, Labour Against the War (UK) and D.C. Labor for Peace and Justice. All union members opposed to Bush's war should attend.</P>
<I><P>For information about NYCLAW and the October 19 conference, go to www.egroups.com/group/LaborAgainstWar.</P>
