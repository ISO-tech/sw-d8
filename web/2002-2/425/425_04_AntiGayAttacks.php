<P><font face="Times New Roman, Times, serif" size="4">Attacked with a baseball bat for being gay</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Stand up to these bigots</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 11, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>, </P>
</B><P>On September 22, a 55-year-old gay man was viciously attacked and beaten by two men in West Hollywood, Calif. According to the Los Angeles Sheriff's Department report, "The two assailants began yelling anti-gay epithets at the victim as they hit him numerous times in the head, face and body."</P>
<P>A passing cab driver stopped and chased the two men away, then drove the victim home. The victim waited 14 hours before seeking medical treatment--possibly "because he was afraid of being identified," said the sheriff's department.</P>
<P>It's hardly surprising why. This was the fourth attack on a gay man in West Hollywood since September 2. On that day, two separate attacks occurred. One was against two men. The other was against a 33-year-old actor named Trev Broudy, who was beaten with a lead pipe and baseball bat--and left in a coma.</P>
<P>About one-quarter of West Hollywood's 36,000 residents are gay. All of the crimes have occurred in the same area of the city and are believed to have been committed by the same two men.</P>
<P>The city's solution to these attacks? Increase late night patrols in the area from 12 to 30 police deputies.  But considering the often hostile attitude of police towards gays, a cop police presence is not the solution to anti-gay hate crimes. </P>
<P>Greater community awareness is a part of it, but searching local newspapers, I only found only two short articles about these earlier attacks.  Jeff Kim of the LA Gay and Lesbian Center told the <I>LA Weekly,</I> "There are more than 1,000 hate crimes a year. The LAPD is not going to put out a press release for every single crime." </P>
<P>As State Sen. Sheila Kuehl said, "These predatory hate mongers count on silence to protect them, just as rapists used to, and we must respond as a community."</P>
<B><P>Evan Kornfeld,</B> Los Angeles</P>

