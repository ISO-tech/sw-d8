<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Bush fiddles while economy fizzles</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 11, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ECONOMIC ANALYSTS are ringing alarm bells about the frightening state of the U.S. economy. In early October, the <I>Economist </I>magazine worried about the tanking U.S. stock market--and warned that "America's manufacturing industry appears to be tipping back into recession." The International Monetary Fund's World Economic Outlook predicts that the pace of the U.S. recovery "is likely to be significantly more moderate than earlier thought."</P>
<P>But don't worry, says George W. Bush. The economy's "fundamentals" are "strong." That's cold comfort for working people. "From the point of view of an average person, we're still in a recession," says Ethan Harris, a chief economist at Lehman Brothers. "What the average person cares about is jobs."</P>
<P>In fact, when Bush made a speech about the economy to workers at a Sears factory in Iowa last month, he had only one concrete proposal. Extended unemployment benefits? A jobs program? Nope. Bush promised to make his package of mammoth tax cuts for the rich "permanent."</P>
<P>Under legislation passed last year--with Democrats providing the margin of victory--the richest 1 percent of taxpayers will rake in 52 percent of the benefits over 10 years. Meanwhile, the bottom 80 percent of taxpayers have already seen three-quarters of the tax relief that they're slated to get--an average of about $350 each.</P>
<P>Bush claims that his tax breaks have come at just the right time to stimulate the stagnating economy. But the lion's share of the cuts won't take place until later in the decade. And they'll go only to the superrich--people with an average income of $1.5 million year.</P>
<P>According to the administration, these lucky few taxpayers will invest the money that they don't hand over to the government, creating new jobs and kick-starting the economy. But no one is going to make new investments if they don't see the prospect of a decent profit--and Corporate America's profit rates are far below the level of the 1990s boom. Bush's tax breaks for the rich may finance a third home in Aspen or redecorate a wing of the mansion--but that won't produce a significant increase in jobs.</P>
<P>The Democrats could easily up the pressure on Bush to cancel the tax breaks. But no mainstream figure has done so. Even Al Gore--who has gone the farthest among leading Democrats in raising questions about Bush's "war on terrorism"--refused to call for the tax cuts to be repealed. Why? Because that would embarrass all the Democratic members of Congress who voted for them last year.</P>
<P>Not only do less than half of U.S. workers qualify for unemployment, but 2.3 million of those who do qualify exhausted their benefits over the last six months. And U.S. Census Bureau statistics show that 1.4 million more people lost their health insurance last year.</P>
<P>Bush's tax giveaway to rich should be canceled right away--to pay for programs that working people desperately need.</P>

