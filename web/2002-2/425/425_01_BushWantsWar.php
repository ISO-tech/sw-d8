<P><font face="Times New Roman, Times, serif" size="4">Bush wants war</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>We say no!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 11, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH gang will say anything to get their war on Iraq. On Monday night, George W. Bush packed the last year's worth of lies and distortions into a speech that was supposed to "make the case for war."</P>
<P>Bush denounced Saddam Hussein for having used "chemical weapons to kill thousands of people." But he didn't mention the more than 1 million Iraqis killed during the last decade by U.S. bombs and a murderous system of Washington-backed economic sanctions.</P>
<P>Bush warned Iraq's generals that "all war criminals will be pursued and punished." This from the man whose administration has demanded immunity for U.S. soldiers from war crimes prosecution by the newly formed International Criminal Court.</P>
<P>He accused the Iraqi government of trying to rebuild an arsenal of "weapons of mass destruction"--even though Washington's doomsday arsenal is far bigger than the rest of the world's combined.</P>
<P>Bush said that Iraq's war on Iran in the 1980s showed why it was a "threat to peace"--but chose to ignore the fact that the U.S. government backed Saddam in that war.</P>
<P>Bush and his buddies have some nerve prattling on about "freedom and democracy." Just look what they have in store for Iraq after "regime change"--a new regime made up of "corrupt, feckless and downright dangerous" thugs, as Scotland's <I>Sunday Herald</I> put it.</P>
<P>Take Gen. Nizar al-Khazraji--a leading contender to replace Saddam. Al-Khazraji was the top commander of the Iraqi army from 1980 to 1991. He was the field commander in charge of the chemical weapons attack that killed and injured 5,000 Kurds in the northern Iraqi town of Halabja in March 1988--the very attack that Washington hawks repeatedly refer to when insisting that Saddam must be toppled.</P>
<P>The hypocrisy is sickening. But almost no one in the Washington establishment is willing to expose these liars. With few exceptions, the Democratic "opposition" in Congress is ready to jump on Bush's war bandwagon. They may squawk about the details--but when Congress votes on the White House's war resolution in the coming days, the Democrats will provide the margin of victory.</P>
<P>Fortunately, growing numbers of people are questioning Bush's insane drive to war--at home and abroad. As many as 100,000 turned out to antiwar protests last weekend, in cities stretching from one end of the country to the other. Almost everywhere, organizers said the turnout was far bigger than they had expected.</P>
<P>Now, activists are using that momentum to build for the national antiwar protest on October 26 in Washington, D.C. That's where we can send a clear message to George Bush and the rest of Washington's war makers: "We say no to war on Iraq!"</P>

