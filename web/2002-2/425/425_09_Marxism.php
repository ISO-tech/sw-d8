<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Marxism is more relevant than ever</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 11, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ON THE 150th anniversary of the <I>Communist Manifesto</I> in 1998, an investment banker told the <I>New Yorker, </I>"The longer I spend on Wall Street, the more convinced I am that Marx was right. I am absolutely convinced that Marx's approach is the best way to look at capitalism."</P>
<P>Perhaps the banker read the <I>Communist Manifesto,</I> which reads almost like it was written yesterday: "[The bourgeoisie] has resolved personal worth into exchange value, and in place of the numberless indefeasible chartered freedoms, has set up that single, unconscionable freedom--Free Trade. In one word, for exploitation, veiled by religious and political illusions, it has substituted naked, shameless, direct, brutal exploitation."</P>
<P>Or perhaps he read the <I>Manifesto </I>on capitalist economic crisis: "In these crises there breaks out an epidemic that, in all earlier epochs, would have seemed an absurdity--the epidemic of overproduction. Society suddenly finds itself put back into a state of momentary barbarism; it appears as a famine, a universal war of devastation had cut off the supply of every means of subsistence; industry and commerce seem to be destroyed. And why? Because there is too much civilization, too much means of subsistence, too much industry, too much commerce."</P>
<P>No doubt the investment banker would be less charitable toward Marx's ideas about getting rid of the capitalist system--that the workers who produce the profits that investment bankers rake in should collectively seize that wealth.</P>
<P>It's one thing to point out that the system has "problems," and another to argue that if we are to survive, capitalism--through its own internal contradictions--must make way for a higher human form of existence, without class distinctions, without poverty and without war.</P>
<P>Marx and Engels wrote the <I>Manifesto </I>not as an academic exercise, but as a call to action. Its aim was not only to expose the system's failings, but to show how it could be transformed by the collective action of the mass of workers and oppressed people. </P>
<P>Of course for every banker enthusiastic about Marx's theory of crisis, there's a "renowned" figure proclaiming that "Marxism is dead." The latest round of assertions came after the collapse of the Soviet Union in 1991. "Socialism is dead! Capitalism is triumphant!" It's time for the "peace dividend."</P>
<P>This assertion came amid a growing gap between rich and poor the world over, where the assets of the top three billionaires are more than the combined GNP of all the least developed countries, with a population of 600 million people. In the U.S., profits and CEO salaries skyrocketed in the 1990s, while workers' wages stagnated or slumped.</P>
<P>Notwithstanding claims about the end of world conflict, our world has become increasingly dangerous, where powerful nations like the U.S. threaten the world with war to maintain their empire.</P>
<P>So, try as they may to dismiss him, Marx keeps coming back. His ideas are alive because his indictment of capitalism--that it is a class society that creates great wealth for the few, the rich capitalists, at the expense of the many, the working class; that it is a society prone to economic crisis and war that create misery for millions--continues to be confirmed on a daily basis.</P>
<P>As the misery worsens, the glaring class divisions give rise to what Marx argued was always the motor of historical change. "The history of all hitherto existing societies," Marx and Engels wrote in the first sentence of the <I>Manifesto,</I> "is the history of class struggle."</P>
<P>Moreover, those who loudly applauded the fall of Stalinism left out one important factor: The death of what passed for communism in the East--but what was in reality bureaucratic, state capitalism--paves the way for masses of people to rediscover the real Marxist tradition behind years of distortion that was encouraged both East and West.</P>
<P>Far from being dead, Marxism is experiencing a rebirth.</P>

