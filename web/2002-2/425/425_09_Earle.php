<P><font face="Times New Roman, Times, serif" size="4">Steve Earle takes on Bush's "war on terrorism"</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>A voice for our side</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Brian Belknap</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 11, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B>MUSIC:</B> Steve Earle, <i><b>Jerusalem,</b></i> Artemis Records, 2002.</font>
<font face="Times New Roman, Times, serif" size="3"><P>IN 1986, Steve Earle was the talk of Nashville when the title track of his first record, <I>Guitar Town,</I> was a Top 10 hit on the country charts. He was hailed as the new Hank Williams and the hillbilly Bruce Springsteen.</P>
<P>Two months ago, Earle was the talk of Nashville again. This time, they were howling for his blood with the release of the song "John Walker's Blues"--about John Walker Lindh, the much-publicized "American Taliban" arrested in Afghanistan--on his latest album, <I>Jerusalem</I>.</P>
<P>Leading the charge was right-wing talk radio host Steve Gill, who said that the song "celebrates and glorifies a traitor to this country&#133;This puts him in the same category as Jane Fonda and John Walker and all those people who hate America." Gill called for a boycott of the album.</P>
<P>The <I>New York Post</I> took up the cry with the headline, "Twisted Ballad Honors Tali-Rat." Then the <I>Wall Street Journal</I> joined the fray with two articles damning Earle--one implying that the song was nothing but a cheap publicity stunt.</P>
<P>The fact is that Earle has for years taken up unpopular causes. Earle has a talent for what's always been country music's strong suit--talking honestly about working people doing the best they can in difficult circumstances.</P>
<P>The song "Billy Austin" on his 1990 record <I>The Hard Way</I> is the first-person story of a poor kid who holds up a liquor store and ends up killing the person behind the counter. The kid makes this observation as he's led off for execution:</P>
<P>Now my waitin's over<BR>
As the final hour drags by<BR>
I ain't about to tell you<BR>
That I don't deserve to die<BR>
But there's 27 men here<BR>
Mostly black, brown and poor<BR>
Most of 'em are guilty<BR>
Who are you to say for sure?</P>
<P>After that album, Earle was contacted by death row inmates and anti-death penalty activists. He became an unflinching opponent of the death penalty, contributing to the <I>Dead Man Walking</I> soundtrack.</P>
<P>Part of Earle's motivation for writing "John Walker's Blues" was the lynch-mob atmosphere around the case. "I don't condone what he did. Still, he's a 20-year-old kid. My son Justin is almost exactly Walker's age. Would I be upset if he suddenly turned up fighting for the Islamic Jihad? Sure, absolutely," says Earle. "But there are circumstances&#133;He was a smart kid, he graduated from high school early, the culture here didn't impress him, so he went out looking for something to believe in."</P>
<P>Earle spent months reading about Islam and Walker before writing the song. Both beautiful and caustic, it starts, "I'm just an American boy raised on MTV/And I've seen all those kids in the soda pop ads/But none of 'em looked like me." Earle sings the chorus in English and Arabic. The song fades out into a haunting reading from the Koran.</P>
<P>Earle has critics from the left as well. The <I>Village Voice</I>'s Robert Christgau criticizes Earle for "lack of subtlety" and compares "Walker's Blues" unfavorably to Bruce Springsteen's "Paradise" from his recent <I>The Rising</I>.</P>
<P>"Good songs generally enhance understanding better than flawed ones," Christgau wrote. "So I prefer not only 'Paradise' but Toby Keith's 'Courtesy of the Red, White and Blue (The Angry American),' with its truthful and jovially vindictive 'Soon as we could see clearly/Through our big black eye/Man, we lit up your world/Like the Fourth of July.'"</P>
<P>This criticism is similar to the barbs hurled at antiwar activists. Just like Earle, we're written off as unsophisticated and lacking subtlety. And--surprise, surprise--Christgau reveals in his review that he's one of those "leftists who believe the U.S. was morally obliged to invade Afghanistan."</P>
<P>At times, Earle rightly makes no attempt at subtlety at all--as with his opposition to the coming war on Iraq. "We intended to go into Iraq before September 11, and we're gonna go into Iraq, and that's part of this big lie," Earle argued in a recent interview. "Iraq had fuck all to do with September 11! John Walker Lindh had fuck all to do with September 11! It's just scapegoating, and scapegoating is always about making somebody feel more than, by making somebody else feel less than--and that's a really dark, dangerous, malignant thing to do."</P>
<I><P>Jerusalem </I>is a richly textured blend of country-punk with elements of hip-hop and R&amp;B that manages to take on everything from the war on terrorism to the criminal justice system and the health care system.</P>
<P>Earle has long been a voice for our side. And his latest album is a wonderful message of hope and compassion--a great antidote to the current atmosphere of cynicism and despair.</P>

