<P><font face="Times New Roman, Times, serif" size="5"><b>Why I'm saying "no" to Bush's war drive</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 11, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>, </P>
</B><P>I am a Vietnam veteran, having served during 1967-68. Now, lacking the adventurousness of my youth (but having the wisdom of politics and middle age), I would think long and hard about following this Commander-in-Chief into an elementary school classroom to break up a fight--let alone following him into a major war that could end pitting the U.S. against the entire Arab world.</P>
<P>Americans who advocate this war at this stage may want to send body bags to the war effort with their own names on them--and be there to claim the young men and women whose bodies will arrive home at U.S. airports. </P>
<P>Maybe they will feel justified in the name of the homeland. I want atonement for September 11, but I want hard facts and intellectual reason and action to guide me--not a cowboy.</P>
<P>If we have to fight in the cities of Iraq, the number of dead will be great. Not to mention an entire Arab world that will have many more generations to hate Americans.</P>
<P>If you feel uncomfortable about picking up a body bag at the airport, it's most likely because you have not gathered all the facts and feel that not all efforts have been made by "the cowboy" to avert war.</P>
<B><P>Dave Yettaw,</B> former president, United Auto Workers, Local 598</P>

