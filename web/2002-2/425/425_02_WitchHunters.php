<P><font face="Times New Roman, Times, serif" size="5"><b>Internet site for witch-hunting the critics of Israel</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 11, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE WITCH-HUNTERS are at it again. Last month, the conservative pro-Israel group Middle East Forum (MEF) unveiled an Internet site called "Campus Watch" that, according to its home page, "monitors and critiques Middle East studies at colleges and universities in North America, with an aim to improving it." </P>
<P>Or, to put it more accurately, tries to squelch any criticism of Israel on college campuses. The site boasts rabidly right-wing reports that attack groups and individuals organizing for Palestinian rights. As usual, anyone who opposes Israel is labeled an "anti-Semite."</P>
<P>The MEF claims that the project has "full respect for the freedom of speech of those it debates"--yet everything they've done so far points to the opposite. In fact, the site originally posted "dossiers" on eight professors and academics who dare to criticize Israel's policies--and claimed that the eight were "hostile" to America.</P>
<P>The MEF also asks for reports of any new "offenders" to add to the list. That practice so outraged professors and academics across the country that, in a show of solidarity, more than 100 professors asked that the Web site add their own names to the blacklist.</P>
<P>"If a group establishes a Web site and says, 'We are watching you,' that has a very chilling impact on academic freedom," Judith Butler, a professor of comparative literature at the University of California-Berkeley, told the <I>San Francisco Chronicle</I>. Butler was one of the first to ask that her name be added to the list. "The more people who actively volunteer themselves for such a list, the less that power of intimidation works."</P>
<P>Since the initial flurry of negative press, the MEF removed the eight "dossiers" and added a disclaimer about the content of each article being the "opinion of the author." That doesn't mean, though, that the group has stopped posting every right-wing opinion piece it can find--and labeling professors and even some students who defend Palestinian rights as "terrorists" and "terrorist supporters."</P>

