<P><font face="Times New Roman, Times, serif" size="4">Evidence against six came from the Web</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"Sleeper cell" or Feds' victims?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 11, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>FEDERAL OFFICIALS have charged six men from New York state with being part of an al-Qaeda "terrorist sleeper cell"--and convinced a judge to deny them bail. Yet the main piece of evidence against the six is their "possession" of a document that is widely available on the Internet.</P>
<P>Last month, the Feds arrested the six and crowed that they had "identified, investigated, and disrupted an al-Qaeda-trained cell on American soil," as U.S. Deputy Attorney General Larry Thompson put it.</P>
<P>According to prosecutors, the men attended an al-Qaeda training camp in the spring of 2001 and then returned to the U.S. to wait for instructions to carry out an attack. But government officials admit they have no evidence that the men were plotting an attack, stockpiling weapons, receiving money or even contacting overseas terrorists.</P>
<P>At a bond hearing, prosecutors resorted to quoting from a document advocating suicide bombings that was found in the home of one of the men, Yasein Taher. The document, titled "The Islamic Ruling on the Permissibility of Martyrdom Operations," is available from many Web sites around the Internet. It has nothing to do with al-Qaeda, September 11, or the U.S. Its main focus is on Muslim rebels fighting Russian government forces in Chechnya.</P>
<P>As his lawyer, Rodney Personius, points out, the 24-year-old Taher is hardly a likely candidate to be a terrorist. He has a wife and a 3-year-old daughter, and is a former high school soccer star. He was even voted "friendliest" in his senior class.</P>
<P>According to Personius, the document dates from two years ago--when Taher was preparing to get married to his high school sweetheart, a Christian, and had decided to learn more about religion. In fact, the folder that the document was found in contained nine other religious documents, also culled from the Internet, on various aspects of Christianity and Islam.</P>
<P>But prosecutors are on a witch-hunt, and they won't stop until they can railroad somebody. "Why use selective citations?" Personius asked. "Why not let [the judge] see the whole thing? I think it's the paucity of evidence against [the defendants], and how the government will take anything they can get their hands on and use it selectively."</P>

