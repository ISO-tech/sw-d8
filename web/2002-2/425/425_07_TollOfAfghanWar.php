<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The cruel toll of the war on Afghanistan</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 11, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE FIRST anniversary of the day that U.S. planes began bombing Afghanistan passed virtually unnoticed on October 7--in stark contrast to the media fanfare that accompanied the first anniversary of the terrorist attacks on September 11.</P>
<P>In an article entitled "One Year On, U.S. Says Afghan War Not Over," Reuters--one of the few U.S. media outlets to acknowledge the first year of the war on Afghanistan--summed up the casualty figures: 40 U.S. soldiers killed (without disclosing the embarrassing fact that most were killed not in combat but from accidents and friendly fire). The subject of Afghan dead and wounded didn't merit even a mention.</P>
<P>Apparently, some lives are worth more than others. Colin Powell said as much during the Gulf War in 1991, when the then-chair of the Joint Chiefs of Staff was asked how many Iraqis had been killed. "It's not really a number I'm terribly interested in," Powell replied.</P>
<P>Few Americans ever learned the actual Gulf War death toll--200,000 Iraqis, according to official estimates that were quickly buried by the media.</P>
<P>Thousands of Afghans have been killed by the 10,000 tons of U.S. bombs dropped in the last year. Britain's <I>Guardian</I> newspaper estimated in February that up to 8,000 Afghans were killed by the bombing.</P>
<P>In May, <I>Guardian </I>reporter Jonathan Steele estimated that up to 20,000 more Afghans died as an indirect consequence of the bombing--from starvation, cold and disease. "They, too, belong in the tally of the dead," wrote Steele.</P>
<P>According to these figures, the war in Afghanistan killed up to nine times the number of Americans killed on September 11--without nabbing the alleged mastermind of the attacks, Osama bin Laden.</P>
<P>And while the U.S. claimed to be fighting on behalf of starving Afghans and women oppressed by the Taliban, a year later, little measurable progress has been made for the mass of the population. As winter approaches, 7 million Afghans are at risk for starvation--the same number as a year ago.</P>
<P>Most promises of humanitarian aid have yet to materialize. The U.S. itself has promised only $300 million to help rebuild Afghanistan--compared to $1 billion a day that the U.S. military spent bombing the country last fall.</P>
<P>The Ministry for Women's Affairs--trumpeted by Bush as proof that the war helped Afghan women--has received next to nothing in financing. The U.S. contributed a mere $120,000 to help the Bureau set up Afghan women's centers. That's "about one-tenth the cost of a single Cruise missile," wrote Rahul Mahajan, the Texas Green Party gubernatorial candidate.</P>
<P>And while 1.5 million Afghan children now attend schools--one-third of them girls--more than 3 million children do not.</P>
<P>Opium production, banned by the Taliban, has climbed to 2,700 tons this year--making Afghanistan the world's leading heroin supplier once again. The warlords in control throughout most of the country--and backed by the U.S. military--run the opium trade in their individual fiefdoms, enriching themselves and funding their private armies.</P>
<P>But perhaps most damning of all is the news that the U.S. was planning to bomb Afghanistan well before September 11--detailed in a BBC News report on September 18 last year that was not picked up by the U.S. media.</P>
<P>"Niaz Naik, a former Pakistani foreign secretary, was told by senior American officials in mid-July that military action against Afghanistan would go ahead by mid-October," the report said. The events of September 11 provided the U.S. with an excuse to set its sights higher, launching a "war without end"--using Afghanistan as a stepping stone to an attack on Iraq.</P>
<P>If an honest accounting of the war in Afghanistan were offered today, enthusiasm for the "war on terrorism" would be replaced by disgust among a wide layer of Americans. And though the media didn't acknowledge the first year of the Afghan war, tens of thousands of protesters took to the streets across the U.S. to protest the coming war against Iraq--choosing the October 7 anniversary to demonstrate their opposition to both wars. </P>

