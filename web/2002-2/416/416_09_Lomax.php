<P><font face="Times New Roman, Times, serif" size="4">The contributions of musicologist Alan Lomax</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Sharing the music of ordinary people</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Keeanga-Yamahtta Taylor</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 2, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ALAN LOMAX, the legendary musicologist who traveled across the U.S and the world preserving the music of ordinary people, died in Florida last week at the age of 87.</P>
<P>Lomax got his start traveling with his father John Lomax, a pioneer in the field of "collecting" folk music. Together, they traveled the South and the West, collecting songs of the indigent and ostracized, including plantation workers, prisoners and cowboys.</P>
<P>"The prisoners in those penitentiaries simply had dynamite in these performances&#133;" Lomax commented. "There was more emotional heat, more power, more nobility in what they did than all the Beethovens and Bachs could produce."</P>
<P>One of my favorite Lomax discoveries is a recording of prisoners on work duty in Mississippi's Parchman Prison--infamous for using slave labor after the Civil War and the inhumane brutality meted out to Black inmates.</P>
<P>Their songs--which were both created spontaneously and grounded in slave spirituals not more than a generation removed from slavery--are a testament to the inmates' creativity and their determination to survive the prison's extreme brutality.</P>
<P>In his travels, Lomax helped to "discover" Black musicians in the South who otherwise would have gone unknown, such as bluesmen Huddie Ledbetter, otherwise known as Leadbelly, and the legendary Muddy Waters.</P>
<P>In the 1940s, Lomax made recordings of stories and songs by folk musician Woody Guthrie for the Library of Congress, which were later commercially distributed. In them, Guthrie paints a vivid picture of life for the farmers and poor people who lived Oklahoma's "Dust Bowl" during the Great Depression.</P>
<P>In the mid-1930s, Lomax traveled with Black novelist Zora Neal Hurston to the Georgia Sea Islands to record Black musicians in their particular island dialect. Because of the extreme racism of the time and the paranoia of white law enforcement and other racists, Lomax had to darken his face with walnut juice to escape the harassment of whites.</P>
<P>Lomax was clearly politicized by the experiences and stories he recorded during his travels. In 1948, he supported Progressive Party candidate Henry Wallace for president, joining Pete Seeger and Paul Robeson in song during the campaign.</P>
<P>Some have criticized Lomax for receiving all of the attention while the Black musicians he recorded received little or no credit or financial reward. But the central point is that Lomax went out and recorded musicians who would otherwise never have been heard--and we're better off because of it.</P>
<P>Lomax's recordings are widely available, and for those interested in a musical component to American history, I strongly urge you to check them out.</P>

