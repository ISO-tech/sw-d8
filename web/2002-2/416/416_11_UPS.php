<P><font face="Times New Roman, Times, serif" size="4">UPS rank and filers criticize Hoffa's contract</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"Why we're voting no"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 2, 2002 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>TEAMSTERS PRESIDENT Jim Hoffa declared that "Teamster members have achieved the best contract in our union's history at UPS" when he settled three weeks early. But many rank-and-file members among the 210,000 Teamsters at UPS don't see it that way. After finally getting details on the contract from the union, many are talking about the huge giveaways that Hoffa and Co. have handed to Big Brown.</P>
<P>With the two-year freeze on full-time job creation for part-timers, the contract allows UPS to reverse much of the major gains won during the 1997 strike. And the pay increases--from the meager 50 cent increase in part-time starting pay to the inadequate cost-of-living formula--won't close the wage gap between drivers and full-timers in the "combination jobs" created from two part-time positions.</P>
<P>For full-time package-car drivers, the language restricting mandatory overtime is weak. And for their part, full-time, over-the-road "feeder" truck drivers, whose jobs are threatened by outsourcing, will get little or no protection under this contract--and the six-year deal would give UPS plenty of time to outsource even more.</P>
<P>Meanwhile, Teamsters officials at the regional and local level have given other big concessions--including a freeze in the big Central States pension plan.</P>
<P>Separate contracts for Local 705 and 710 are expected to follow much of the national contract.</P>
<P>That's why UPS Teamsters should vote this deal down--and send negotiators back to the table. It's time the union really carried a fight for the deal that UPS workers deserve.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<P>HERE'S WHAT UPS Teamsters are saying about Hoffa's deal with UPS:</P>
<B><P>Part-time loader, Atlanta<BR>
</B>In six years, with this raise, we'll still be broke. [Inflation] goes up, not down. If they want us to work, good, they need to pay us well...I don't want to be broke in five years, I want to be stable. They're gonna keep me on the bus, or in a used car. I cannot be proud on a UPS check. Make me proud of where I work. Right now I'm struggling.</P>
<B><P>Feeder driver, Chicago<BR>
</B>The part-timers got nothing. I was making $12 or $13 in 1979.</P>
<B><P>Car washer, Chicago<BR>
</B>The members have to be deaf, dumb and blind to vote yes for it. There are not enough full-time jobs being created. Instead of a better contract than 1997, we are going backwards. UPS is going forward on profits. I hope the members remember Hoffa's statement that this is the best contract in UPS history when it comes to his re-election.</P>
<B><P>Air driver/sorter combination worker, Chicago<BR>
</B>If we don't improve the language we are going to get screwed. We should make one pay scale across the board. Not two pay scales. We're full-time with no real full-time positions.</P>
<B><P>Package driver, Manhattan<BR>
</B>Six years is way too long. Right now, if you don't know your rights, management tries to violate the contract every day. In six years, they'll act like there is no contract. </P>
<B><P>Feeder driver, Long Island<BR>
</B>It may sound good to the public with the 22 percent raise, but look closer. Mandatory overtime? People shouldn't work that if they don't want to. We haven't had a real COLA in years. Six years? The cost of living can go up 30 percent in six years.</P>
<B><P>Package driver, Long Island<BR>
</B>We deserve a lot more. UPS is a very wealthy company. We gave in too soon, too early. What we won in '97 gave us a fear factor with the company. We had them backed into a corner and we're letting them escape. I'd recommend a "no" vote. </P>
<B><P>Air walker, Manhattan<BR>
</B>President Hoffa said UPS is a model company at the press conference. That shows he's never worked for the company. Now the union and company are both trying to sell us this contract. I don't expect anything from UPS, but my union should at least be giving us the straight story. Instead it's all advertising and PR.</P>
<P>They say part-timers get a $6 raise. But actually, the start pay goes up only 50 cents for six years, and that's the only wage most part-timers will ever see. So in 2008, part-timers would be making $8.50-$9.00 for breaking their backs every day.</P>
<P>How will we ever convince Fed Ex workers to join the union when the majority of UPS Teamsters--part-timers--are working for slave wages?</P>

