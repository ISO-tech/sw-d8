<P><font face="Times New Roman, Times, serif" size="4"><b>A bunch of grouches</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>FIRST IT was Jerry Falwell, flipping out over a supposedly gay Teletubby. Now, congressional Republicans are having a fit over an HIV-positive Muppet character being developed for Takalani Street, the South African version of Sesame Street. </P>
<P>Producers say that the new character is intended to challenge stigmas of HIV-infected people in a country where an estimated one in nine people is infected.</P>
<P>When the new character was announced, Rep. W.J. ''Billy'' Tauzin (R-La.) and five other Republicans on the House Commerce Committee wrote to PBS President Pat Mitchell, expressing "concern" that the new character would be inappropriate for young U.S. viewers. In a barely veiled threat, the letter noted that the lawmakers' committee controls PBS's funding. </P>
<P>Mitchell replied immediately, saying that there are no plans to bring the character to the U.S. show.</P>
<P>--<I>Entertainment Weekly,</I> July 18, 2002</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Fighting terror at summer camp</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ARE YOUR kids bored with the same old summer camp? Now they can attend the nation's first "homeland security training summer camps for teenagers."</P>
<P>The program--called "Secure Corps" in Bucks County, Pa.--is drilling 92 teenagers in "essential skills" this summer.</P>
<P>When the eight-week program ends on August 23, graduates will be certified in first aid, CPR (both human and animal)--and what organizers call "terrorism response." They will be ready to don haz-mat chemical-protection suits, evacuate institutional buildings and become "first responders" to highway pileups and poison-gas attacks.</P>
<P>And forget about evenings spent roasting marshmallows. Each group of campers has been assigned a long-term homeland security problem to study, such as what to do if there's a subway derailment or a bomb threat at a mall.</P>
<P>"I've had enough of this terrorism," camper Mark Tomlinson told reporters.</P>
<P>--<I>Philadelphia Inquirer,</I> July 24, 2002</P>

