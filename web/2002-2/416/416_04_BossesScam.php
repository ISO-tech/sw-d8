<P><font face="Times New Roman, Times, serif" size="4">How corporations profit from workers' deaths</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The bosses' sick scam</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 2, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>Companies are always looking for ways to extract surplus value from their employees, but I don't think that even Marx could have anticipated one of the recent scams U.S. companies have cooked up. </P>
<P>Some companies have been taking out life insurance policies on low-income employees--without informing them. These policies are officially known as Corporate-Owned Life Insurance (COLI), but within the insurance industry they're often referred to as "dead janitor's insurance" or, more colorfully, "dead peasant's insurance."</P>
<P>Recently, Jane Sims found out that when her husband Douglas, who had been a Wal-Mart employee for 11 years, died of a heart attack, the company received $64,000--not a dime of which went to her or her family. "I never dreamed that they could profit from my husband's death," she told the <I>Houston Chronicle</I>. It seems that Wal-Mart has taken out 350,000 life insurance policies on employees. </P>
<P>National Convenience Stores received $250,000 when one of its employees was killed in a robbery. An attorney for Hartford Life Insurance, which sells such policies, estimated that one-fourth of the Fortune 500 companies have them, and they cover the lives of between 5 million and 6 million people. </P>
<P>Some companies use the money from COLI to pay for benefits for their executives. Portland General, a subsidiary of Enron, uses money from COLI to pay for long-term compensation and for supplemental retirement payments for its executives. The company has $80 million in a trust fund for this purpose. </P>
<P>Of course, many of Portland General's workers lost their life savings, invested in 401(k) plans, when Enron went bankrupt.</P>
<P>Currently, only five states require employee consent for COLI. A bill was recently introduced in Congress that would require employee consent in all states for the insurance. </P>
<P>These policies, however, should be outlawed. Companies that exploit workers shouldn't be able to benefit from their deaths, too. </P>
<B><P>Evan Kornfeld,</B> Los Angeles</P>

