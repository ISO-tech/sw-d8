<P><font face="Times New Roman, Times, serif" size="4">Bush cuts funds for UN family planning agency</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Another present for the fanatics</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 2, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"WOMEN AND children will die because of this decision." That was the response of Thoraya Obaid, executive director of the United Nations Population Fund (UNFPA), to the Bush administration's decision to take away $34 million in funding for the international agency. The decision represents another present for anti-abortion bigots from their friend in the White House.</P>
<P>The UNFPA operates projects that provide contraception and gynecological services, teen-pregnancy prevention and HIV/AIDS prevention in 142 countries. For years, right-wing cranks in the U.S. have accused the fund of giving money to governments--specifically China--that participate in forced sterilization and abortions.</P>
<P>Bush's own State Department has found no evidence of this. But the White House didn't let the facts get in the way of keeping its right-wing supporters happy. On July 21, Secretary of State Colin Powell wrote a letter to the Senate Appropriations Committee, explaining that the administration was withdrawing funding from the agency.</P>
<P>The cost could be devastating around the globe. "In concrete terms, we estimate that $34 million for reproductive health and family planning would be enough to prevent 2 million unwanted pregnancies, nearly 800,000 induced abortions, 4,700 maternal deaths, nearly 60,000 cases of serious maternal illness and over 77,000 infant and child deaths," Obaid said in a statement.</P>
<P>Almost immediately, the European Union and the International Planned Parenthood Federation pledged to make up more than $20 million of the funds. And some Democrats are attempting to get funding restored through congressional means.</P>
<P>But that shouldn't distract attention from this latest anti-choice outrage. Bush and Co. have proved again just how little they care about women's lives.</P>

