<P><font face="Times New Roman, Times, serif" size="4">Pa. accident could have been prevented</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Putting profits before miners' lives</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 2, 2002 | Page 2</font> </P>
<font face="Times New Roman, Times, serif" size="3"><P>PEOPLE AROUND the country greeted news of the rescue of nine trapped miners in Pennsylvania last weekend with relief and joy. But this shouldn't hide a crucial lesson--that this accident was easily avoidable.</P>
<P>The Quecreek Mine accident was the result of a drive by coal operators everywhere to put profits ahead of miners' safety--with cooperation by federal and state regulators. The result has been a jump in fatal mine accidents in each of the last three years.</P>
<P>Last year saw the worst U.S. coal mining disaster in 17 years at the Brookwood mine in Alabama, where a lack of cooling equipment required in other parts of the world may have caused a methane gas explosion that killed 13 men. A total of 42 miners died last year as U.S. coal output hit a record 1.1 billion tons.</P>
<P>In last week's accident in Somerset County, Pa., Black Wolf Coal Co., the nonunion contractor that operates the mine, claimed that inaccurate maps led miners to drill directly into an adjacent abandoned mineshaft filled with water--which then flooded their work area. But Black Wolf wouldn't explain why it didn't use technology to test the accuracy of the maps.</P>
<P>Black Wolf can't claim ignorance. It's run by David Rebuck, an executive at Mincorp Inc., the ultimate owner of the mine. Mincorp owns PBS Coal, which has operated in Somerset County for decades. In turn, PBS controls the company that owns the mine, Quecreek Mining Inc., which subcontracts operations to Black Wolf.</P>
<P>And Mincorp itself is a spinoff of NMS, a British coal operator that went bankrupt under a previous name in an Enron-style accounting scandal in the 1980s.</P>
<P>This maze of subsidiaries and contractors is typical of the coal bosses' efforts to keep out the United Mine Workers of America (UMWA), cut costs and reduce their liabilities for accidents or pensions. The inevitable result is danger and death.</P>
<P>Black Wolf has been cited 26 times for violations of federal mine safety regulations since March 2001--including a non-fatal roof collapse last September. And PBS was cited last year for allowing inexperienced truck drivers to unload coal in freezing conditions, resulting in an accident that left one driver dead.</P>
<P>"Historically, smaller mines have had higher accident rates because they're generally undercapitalized," Joe Main, chief health and safety administrator for the UMWA, told reporters. "They don't have the training, staff or updated equipment. Many don't have a bargaining group to speak up when a company tries to cut corners."</P>
<P>Pennsylvania officials, including Gov. Mark Schweitzer, took credit for the rescue. But the Pennsylvania Department of Mines has been criticized by the UMWA for giving coal operators  "variances" --that is, exemptions--from mine safety standards.</P>
<P>The federal Mine Safety and Health Administration is no better. It has cut back on inspections and recently ruled that companies could use high-voltage equipment underground, even though many recent mining fatalities were electrocutions.</P>
<P>The nine miners at Quecreek survived because of the heroic work of rescue workers. But fatal mine disasters are inevitable as long as profits are put ahead of workers' rights and safety.</P>

