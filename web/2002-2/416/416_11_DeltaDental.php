<P><font face="Times New Roman, Times, serif" size="5"><b>Workers at Delta Dental strike against givebacks</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Andy Libson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 2, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SAN FRANCISCO--Some 1,100 employees of Delta Dental insurance have been on strike since July 19. Workers, members of Teamsters Local 856, have set up picket lines in downtown San Francisco, as well as the company office in Sacramento.</P>
<P>Like many companies, Delta Dental is using the excuse of economic downturn to try to extract massive concessions from workers. Delta is by far the largest dental insurance provider in California, handling more than $6.2 billion worth of claims a year.</P>
<P>Now, Delta Dental wants more. The company is pushing workers to accept a $2,000 cap on dental coverage, high co-payments and longer hours. Delta Dental wants employees, who currently work 37.5 hours a week, to work additional hours on Saturday--and without overtime pay.</P>
<P>The company offered workers a 9 percent wage increase, but this is next to nothing considering that hours will increase more than 6 percent. "Most of us are single mothers who have to find day care for our kids," said Delta Dental worker Valerie Warren. "It's bad enough they are forcing us to may more for health care for our families, but forcing us to work on Saturdays is too much. Do you know how difficult it is do find child care on the weekends?"</P>
<P>Warren described workers' last contract. "We got a five-year contract with only a 5 percent wage increase," she said. "That's nothing. We want to get some of that back. This company has the money. They just want it for themselves."</P>
<P>Workers' determination to fight is strong. They voted down the most recent contract offer 264-10. Unfortunately, workers report that they aren't just fighting the company but the union as well. Workers called union leaders "unsupportive," and said that they are using all sorts of tactics to get them to agree to the latest contract. Instead of having workers vote on their contract together as they have wished, union leaders forced them to vote separately, 20 people at a time.</P>
<P>When workers in San Francisco resoundingly defeated the last offer, union leaders told workers in Sacramento that they'd accepted the offer. Local 856 member Darro Jin says the union is "corrupt" and "talks more with the management than with us."</P>
<P>Workers are right to fight against Delta Dental's greed, and will need to maintain the unity they have shown. But it is also clear that they will have to fight to make their union work for them.</P>

