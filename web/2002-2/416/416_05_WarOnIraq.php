<P><font face="Times New Roman, Times, serif" size="4">Leaks show all-out assault is a matter of when, not if</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The coming war on Iraq</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 2, 2002 | Page 5</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>ELIZABETH SCHULTE reports on new revelations of the Bush gang's plan for a war on Iraq.</P>
</B><P>SOME 250,000 U.S. Marines and soldiers. Hundreds of warplanes based in as many as eight countries. An air assault against thousands of targets, including airfields, roadways and communications sites. Special Operations forces or CIA agents striking depots or laboratories storing suspected "weapons of mass destruction."</P>
<P>These are a few of the details of the U.S. government's plan for a war on Iraq, as described in a Pentagon document leaked to the <I>New York Times</I> at the beginning of July. Defense Secretary Donald Rumsfeld says that he's determined to sniff out the source of the leak. But since September 11, it has been anything but a secret that Rumsfeld and the rest of George W. Bush's war team are itching to attack Iraq.</P>
<P>First, there were the frenzied attempts to link Iraqi leader Saddam Hussein with the September 11 hijackers. Then the equally frenzied effort to blame Iraq for the anthrax attacks last fall. That story fell through, too, but Bush continued his witch-hunt, naming Iraq as part of "the axis of evil" in his State of the Union address.</P>
<P>The regular charge is that Saddam and his regime are assembling "weapons of mass destruction." Unfortunately for the hawks, there's little evidence of this. Iraq has been in a state of virtual collapse since the 1991 Gulf War, not only because of U.S. bombs, but also because of more than a decade of economic sanctions.</P>
<P>But the Bush gang isn't letting the truth get in the way. In June, the administration leaked its plan to use Special Operations forces to assassinate Saddam. The leak served the purpose of convincing the Iraqi government that proposed new teams of United Nations (UN) weapons inspectors might well contain killers.</P>
<P>As former inspector Scott Ritter wrote in the <I>Los Angeles Times,</I> "Absent any return of weapons inspectors, no one seems willing to challenge the Bush administration's assertions of an Iraqi threat&#133;The true target of the supposed CIA plan may not be Hussein but rather the weapons inspection program itself. The real casualty is the last chance to avoid bloody conflict."</P>
<P>The administration's saber rattling at Iraq, whether openly or through leaks to the media, serves another purpose--shifting attention away from the rotten economy and White House connections to corporate crime.</P>
<P>That's why not every round of stories about the Pentagon's invasion plans should be taken as a sign that an attack is coming soon. But the White House wants a war.</P>
<P>It faces some roadblocks, though. Dick Cheney's tour of the Middle East earlier this year to drum up support ran into stronger-than-expected opposition from U.S. allies among Arab governments.</P>
<P>The trip came just as Israel was escalating its terror campaign against Palestinians. Arab leaders who want the U.S. to topple Saddam nevertheless won't openly support Washington if they know this could spark massive anger from below.</P>
<P>Even European leaders have been cautious. Bush's loyal stable boy, British Prime Minister Tony Blair, was careful last week to announce that, while a regime change was "desirable," the objective was "getting the inspectors back in."</P>
<P>A U.S.-sponsored "regime change" in Iraq will be harder to accomplish than the quick "victory" in Afghanistan.</P>
<P>No one can predict when George W. Bush will strike Iraq. The assault could come early next year--or even sooner. But some things are certain.</P>
<P>Bush Junior is determined to "finish" what his father started in Iraq--protect the U.S. government's plunder of oil resources in the Middle East and prove that Washington calls the shots.</P>
<P>Opponents of war in the U.S. have to fight him every step of the way.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Bush's brutal ally in war plans</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>DEPUTY DEFENSE Secretary Paul Wolfowitz made a two-day stop in Turkey in mid-July to make sure that everything was in order for the coming war on Iraq. Things weren't exactly as he might have hoped.</P>
<P>Turkey--which hosts the base for U.S. warplanes that patrol the so-called "no-fly" zone over northern Iraq--is experiencing one of the worst economic and political crises of its history.</P>
<P>On July 16, Prime Minister Bulent Ecevit was forced to announce early elections for November 3, after half his Democratic Left Party members in parliament defected, causing the government to lose its majority.</P>
<P>Ecevit is asking the U.S. to write off Turkey's more than $4 billion debt--and approve a new $228 million aid package. Ecevit also expressed fears that a war against Saddam would encourage Kurds in northern Iraq to create an independent state--which could, in turn, fuel the resistance of Turkey's Kurdish minority.</P>
<P>Turkey has carried out a savage war against the Kurds. Supporters of Kurdish civil rights and independence make up a large portion of more than 12,000 political prisoners who languish in Turkey's nightmarish prisons.</P>
<P>And this is a valued U.S. ally in the "war on terrorism."</P>

