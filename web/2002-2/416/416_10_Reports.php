<P><font face="Times New Roman, Times, serif" size="4"><b>Justice for Palestinians</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Katherine Gorell</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--More than 500 people gathered in midtown Manhattan at the Israeli Consulate July 26 to protest Israel's gruesome F-16 attack on a Gaza neighborhood that killed 15 Palestinians and injured 145 more.</P>
<P>The protest, organized by the Al-Awda Right to Return Coalition, condemned George W. Bush's green light for Israel's terror against Palestinians and called for an end to U.S. aid to Israel. Chanting "Bush, Sharon, you must learn, refugees will return" and "Jewish people, yes; Zionism, no," the protesters then marched several blocks to the offices of New York Sens. Charles Schumer and Hillary Clinton, both major supporters of aid to Israel. </P>
<P>"Blocking ambulances, imposing curfews and implementing many other means of destroying Palestinians' quality of life has become the norm," Reem Abu-Sbaih, an Al-Awda organizer, told <I>Socialist Worker</I>. "People need to understand what occupation is," said Abu-Sbaih. "People who are sick can't get to the hospital, which is a clear violation of basic human rights and international law."</P>
<P>Several other groups also joined the protest in solidarity. "[We're here] to speak from our position of strength as Jews," explained Emmaia Gelman, a member of Jews Against the Occupation, to <I>Socialist Worker</I>. Gelman recently returned from the Middle East where she spent time as an international observer monitoring Israeli military abuses.</P>
<P>The large turnout drove home the point that, even while the mainstream media ignore the plight of Palestinians, there's a growing movement in the U.S. that supports the Palestinian struggle. It's urgent to continue building large demonstrations, teach-ins and divestment campaigns.</P>
<P>Justice for Palestine!</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Amnesty for immigrants</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Brian Huseby</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SEATTLE--Approximately 200 protesters turned out at Seattle-Tacoma International Airport on July 27 to protest INS raids on immigrant workers.</P>
<P>The workers are employed by Sky Chef, which prepares meals for airlines. During negotiations with the Hotel Employee and Restaurant Employee union, the company called a phony employee meeting.</P>
<P>Immigration and Naturalization (INS) agents, dressed in Sky Chef uniforms, were sneaked into the meeting and arrested 10 union members. All of the arrested employees were Latino and Asian--some of whom have worked for the company for up to 10 years. Many are still separated from their families.</P>
<P>The march wound through baggage claim to a rally at an airport lounge. Solidarity was the theme of the rally, as speakers from a variety of labor and human rights groups demanded an end to the raids.</P>
<P>The march resumed through ticketing and check-in amid such chants as "What do we want? Amnesty! When do we want it? Now!" and "Hey, hey, ho, ho. Ashcroft's laws have got to go!"</P>
<P>End INS raids on immigrant workers!</P>

