<P><font face="Times New Roman, Times, serif" size="5"><b>A few good insults and insights from Vidal</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Adam Turl</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 2, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><P><STRONG>BOOKS:</STRONG> Gore Vidal, <I>Perpetual War for Perpetual Peace: How We Got to Be So Hated.</I> Thunder Mouth Press/Nation Books, 2002, 160 pages, $10.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>Gore Vidal's <I>Perpetual War for Perpetual Peace</I> is one of several recent titles debunking the rhetoric that September 11 was an attack on U.S. "freedom" and "democracy." In that sense, the book, which made bestsellers lists, is welcome.</P>
<P>Vidal argues that the origin of the September 11 attacks lies in the brutality of U.S. foreign policy, particularly in the Middle East. To hammer this home, the book includes a chart of dozens of U.S. military actions over the past 50 years, such as the Vietnam War's "Operation Freedom Train."</P>
<P>Also welcome is Vidal's notable wit unleashed on the witless Bush and his corporate paymasters. "Bush was transformed before our eyes into the cheerleader he had been in prep school," Vidal writes. "First he promised not only a 'new war' but a 'secret war' and, best of all, according to the twinkle in his eye, 'a very long war.'"</P>
<P>The book includes a 1998 <I>Vanity Fair</I> article, which outlines how the attack on civil liberties, like the attack on Afghanistan, was in the making before September 11.</P>
<P>But the book has some unwelcome flaws. Vidal considers the present state of affairs a deviation from American values--going back to the Constitution, or to FDR. And he sees the politicians and corporations as personally corrupt, but not the entire system as such. So he argues that Bill Clinton didn't stand up to the military because he dodged the Vietnam War draft, when the truth is that Clinton was always willing to use the military to defend U.S. interests.</P>
<P>The book also contains a long section on Timothy McVeigh and the Oklahoma City bombing in 1995, which rightly looks into the root causes of the bombing, but is largely uncritical of McVeigh. Other material seems unconnected to the subject matter at all, aside from the fact Vidal wrote it.</P>
<I><P>Perpetual War</I> has a few good insights and a few good insults hurled at George W. Bush. But don't expect to get an analysis of why we're in such a mess, or an idea about how to get out of it.</P>

