<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The other party of big business</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 2, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ONE DAY after Democrats crowed that they had forced a White House "unconditional surrender" on their accounting reform bill, the so-called "friends of working people" announced a "compromise" with Republicans on bankruptcy "reform." These two actions, occurring within 24 hours, exposed the two faces of the Democrats.</P>
<P>They draw votes from ordinary people with an appeal to stand up for them. At the same time, the Democrats are a capitalist party that carries out the interests of big business. It's worth keeping this in mind as Democratic politicians spend the next three months appealing for votes.</P>
<P>With George W. Bush and Dick Cheney on the defensive over their hip-deep wallow in corporate scandals, Democrats are noticeably more confident about winning control of Congress in November. Many who seethed for two years as the Bush regime pushed its right-wing agenda will view this positively.</P>
<P>But the amazing factor about this turn of events is that the Democrats aren't leaving Bush and the Republicans in the dust. Most opinion polls on voters' preferences show a relatively even split between Democrats and Republicans.</P>
<P>Some liberals are worried that the Democrats will blow a golden opportunity. "If the Democrats forfeit the opportunity now handed them to connect all the flaming dots, they are truly as flabby, corrupt and venal as Ralph Nader says," wrote Todd Gitlin in the <I>Los Angeles Times</I>. For someone who's made his reputation as a scold of the left--including as a Nader-basher in the 2000 election--this is quite an admission.</P>
<P>Democrats have made a major priority of passing technical legislation to increase "transparency" in corporate accounting. The legislation was so uncontroversial that even most Republicans voted for it.</P>
<P>Meanwhile, Senate Democrats shot down Sen. John McCain's (R-Ariz.) proposal to rein in the orgy of stock options provided to CEOs. And where are the Democratic calls to investigate Bush's and Cheney's business dealings--or legislation to guarantee every American a pension? Nowhere.</P>
<P>The most obvious reason for the Democrats' timidity is their own ties to big business. They don't want to dig too far into the corporate scandals because they're implicated as much as Republicans are.</P>
<P>As the <I>Wall Street Journal</I> recently pointed out, the "party of big business," the Republicans, pocketed $7.4 million in contributions from WorldCom, Enron, Adelphia, Global Crossing and Tyco. The Democrats, the "party of the people," raked in $5.2 million from these same disgraced companies.</P>
<P>Now you know the real difference between Democrats and Republicans. The Democrats' devotion to carrying out business's agenda has remained intact despite their recent posturing.</P>
<P>If Democrats' debts to big business have made them slow to attack, what's the excuse of someone like AFL-CIO President John Sweeney? The rising tide of working-class anger seems unfocused because the organization that could articulate it in basic class terms--the AFL-CIO--remains more committed to the Democrats' electoral goals than to workers' needs.</P>
<P>If the Democratic machine's ties to big business compromise it, Sweeney's ties to the Democratic machine compromise him. At best, Sweeney talks about "working families" as he ritually endorses the standard list of poll-tested Democratic election themes--like support for prescription drug benefits under Medicare.</P>
<P>But seniors who keep voting for Democrats in the hope of seeing these drug benefits before they die are right to be cynical. Democrats always talk about how difficult it is to pass this kind of legislation while they shovel billions to the Pentagon at the drop of a hat.</P>
<P>With every passing day, the Democrats prove that they're as much part of the problem in Washington as the Republicans.</P>

