<font face="Times New Roman, Times, serif" size="5"><b>Ashcroft's Big Brother spy plan</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 2, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>A SNITCH on every block. That's what Attorney General John Ashcroft wants.</P>
<P>The pilot phase of his Terrorism Information and Prevention System--known as Operation TIPS--is due to be launched this month. The Justice Department wants to recruit 1 million people in 10 cities--and give them "a formal way to report suspicious terrorist activity," as the TIPS Web site puts it. In other words, be a spy.</P>
<P>The 1 million would amount to one out of every 24 people in the 10 largest cities in the U.S.--a higher percentage of informants than even the former Stalinist dictatorship of East Germany had, with its infamous Stasi secret police.</P>
<P>TIPS is an open invitation to racial profiling and scapegoating--against everyone from Arab immigrants to political activists. The program's echoes of George Orwell's <I>1984</I> are so obvious that many mainstream newspapers opposed it. Even House Majority Leader Dick Armey (R-Texas)--as right wing a Republican as they come--tried to put the brakes on the citizen spy scheme with a provision in legislation forming the new Department of Homeland Security, which passed the House last week.</P>
<P>But Ashcroft isn't giving up. Last week, he tried to calm the uproar with claims that reported information wouldn't be stored in long-term databases. And Homeland Security czar Tom Ridge declared that "the last thing we want is Americans spying on Americans."</P>
<P>But that's exactly what they want. TIPS is part of the Bush gang's agenda for returning to the days when the government's security apparatus had free reign to target anyone they wanted to.</P>
<P>Of course, the Feds don't need to recruit racist snoops. They've got plenty of their own. Like the Detroit Secret Service goon who was recently suspended for writing "Islam is Evil, Christ is King" on a calendar posted on the refrigerator of a Muslim immigrant whose house was raided.</P>
<P>Ashcroft and the Bush gang exploited September 11 to carry out a witch-hunt. More than 1,000 people, mostly young Arab men, were detained as part of the investigation, and an untold number remain behind bars--but not a single one has been charged with a crime connected to September 11.</P>
<P>With TIPS, the witch-hunters in Washington want to take their attack on our rights to a new level. But the angry response to this latest scheme shows growing numbers of people are starting to question the crackdown. They will come to realize that Ashcroft and his thugs are the real threat--especially if you happen to be of Arab descent.</P>
<P>We have to speak out--and show the Bush gang that we won't let them take away our rights.</P>

