<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Defending their right to torture</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 2, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE INTERNATIONAL Criminal Court. Global warming. Nuclear weapons test ban. Arms control. Land mines. Biodiversity. The sale of small arms. These are a few of the international treaties that the U.S. government has tried to wreck lately.</P>
<P>But just when you thought it was impossible to get any lower, the Bush administration sank to new depths of hypocrisy last week--when it tried to block an international agreement on monitoring torture. Joined by countries that it regularly denounces--including China, Cuba, Iran and Libya--the U.S. demanded that the agreement be re-negotiated.</P>
<P>The "optional protocol" would allow inspections of prisons and other detention centers by international observers--who would judge if countries are abiding by the United Nations (UN) agreement against torture.</P>
<P>The Bush gang conjured up the objection that international inspections would be a violation of "state's rights" under the U.S. Constitution, because observers could enter state prisons, regardless of local laws. But this mumbo-jumbo is a cover. </P>
<P>The Bush gang doesn't want anyone getting into the Guant&aacute;namo Bay naval base, where prisoners from the U.S. war on Afghanistan are being held indefinitely in barbaric conditions that amount to torture. </P>
<P>Or the Immigration and Naturalization Service detention centers, where victims of John Ashcroft's post-September 11 witch-hunt of Arabs and Muslims remain behind bars.</P>
<P>Washington loves to criticize "rogue states" around the world for flouting democracy and committing human rights abuses. Every mainstream politician in the country supports U.S. military action to topple Saddam Hussein in Iraq, for example--the only questions are when and how.</P>
<P>But let someone question the U.S. government--or its ally, Israel, for that matter--and the politicians start singing a different tune.</P>
<P>Earlier last month, the White House succeeded in forcing the UN to accept a compromise on the new international criminal court, exempting U.S. soldiers from prosecutions for war crimes and crimes against humanity for as long as the UN Security Council--where the U.S. has veto power--says so.</P>
<P>Meanwhile, press reports indicate that Pentagon weapons labs have begun preparing to test a new generation of nuclear weapons--so-called "mini nukes" that White House fanatics hope can actually be used. </P>
<P>When it comes to democracy and human rights, the U.S. government has two standards. One for the rest of the world--enforced selectively, depending what serves Washington's interests--and another for itself.</P>
<P>The only international law that the world's top cop recognizes is this: Whatever we say, goes.</P>

