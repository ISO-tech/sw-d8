<P><font face="Times New Roman, Times, serif" size="4">New book chronicles anti-sweatshop movement</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Missing out on the important debates</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by David Thurston, former USAS National Coordinating Committee member</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 26, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>BOOKS:</B> Liza Featherstone, <B><I>Students Against Sweatshops.</B></I> Verso, 2002, 120 pages, $15.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>"WHAT'S DISGUSTING? Union busting! What's outrageous? Sweatshop wages!" This chant and others rang through university administration buildings and Nike stores across the nation in 2000.</P>
<P>Liza Featherstone's <I>Students Against Sweatshops</I> tells the story of United Students Against Sweatshops (USAS), the national network that coordinated this new wave of student activism.</P>
<P>Founded in the spring of 1998, by 2000, USAS had committees or affiliated organizations at several schools. At most schools, activism began with a small group of students who were outraged that the clothes bearing their school's name were made in sweatshops. They believed that, if they exposed the problem, they could convince administrators to take action.</P>
<P>They came to see that their administrations were committed to profiting from sweatshop labor and running their schools with unchallenged authority. Activists responded by organizing sit-ins in 1999. The biggest wave of sit-ins was in the spring of 2000.</P>
<P>For people unfamiliar with the movement, this book offers an introduction to the issues of sweatshop labor and a feel for the movement's excitement. But the book has some major problems.</P>
<I><P>Students Against Sweatshops</I> glosses over most of the movement's key debates. For example, it fails to explain the consequences of consensus decision making--a subject of heated argument at USAS's 2000 summer conference.</P>
<P>Using consensus--in which everyone must agree before a decision can be made--over formal democracy--in which people vote and then accept the majority decision--means that informally and behind the scenes small numbers of people can dominate. So, on decision-making conference calls, real argument about tactics and politics was discouraged because it made it harder to reach "consensus."</P>
<P>Most significantly, the book dances around the question of class. The foreword by Molly McGrath argues that the group aimed "to empower those with almost no power at all."</P>
<P>While some students came to the movement believing they were fighting for workers who couldn't fight for themselves, the best activists came to recognize that the most effective anti-sweatshop agreement is a union contract. In fact, the best parts of the book are stories gathered by USAS members who went on delegations to meet sweatshop workers, where they came to see workers not as victims of the global economy, but as willing and able to fight back.</P>
<P>Unfortunately, Featherstone also panders to red-baiting with a reference to "glassy-eyed sectarian-newspaper sellers." The author also exaggerates the movement's current strength, suggesting that USAS is a force on hundreds of campuses and is now leading the way in building a student antiwar movement.</P>
<P>She fails to take stock of how the global justice movement as a whole was thrown backward by Bush's war drive after September 11. And she ignores the debates that exist in the antiwar movement, from decision making to the politics of pacifism and relating to the Palestinian struggle.</P>
<P>It is unfortunate that the first book-length study of USAS is so flawed.</P>

