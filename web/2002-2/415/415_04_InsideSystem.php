<P><font face="Times New Roman, Times, serif" size="4"><b>White-collar prisons</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WITH WAVE after wave of shady business deals, accounting scandals and outright frauds coming to light, it's just possible that one or two of the biggest corporate crooks might spend a day or two in jail. But never fear. Even if they happen to be convicted, the high-and-mighty can--for the right price--hire a "post-conviction specialist."</P>
<P>For up to $400 an hour, the post-conviction specialist will scope out a prison best suited to a convict's hobbies and "special needs"--and organize glowing reference letters to be reviewed by the sentencing judge.</P>
<P>And if a jail term is handed down to any of the corporate crooks, they might be fortunate enough to serve part of it in the luxury of their own mansions or condos. Like Diana Brooks, the former CEO of Sotheby's auction house, who was sentenced in April for fixing art auctions. Brooks is serving six months of "home confinement" in her 12-room, $5 million co-op on Manhattan's Upper East Side.</P>
<P>--<I>Chicago Tribune,</I> July 14, 2002</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Save Martha! Free the housewares!</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>When actress Winona Ryder was busted for shoplifting, some hipsters began sporting "Free Winona" shirts. Now, trendsetters can checkout SaveMartha.com, a Web site dedicated to supporting the queen of fine living, Martha Stewart.</P>
<P>Stewart has been largely silent about her legal woes stemming from alleged insider trading. But SaveMartha editor John Small says, "A group of Martha Stewart fans got together and decided the kind of negative coverage in the media of top female executives that we've seen recently deserves a response. SaveMartha.com is a place to address this concern...Fans of Martha need a place to come and voice their opinions and concerns."</P>
<P>And, of course, the site also offers merchandise--including T-shirts, mugs and coolers--all with the phrase "Save Martha!" As Small said, "What would a site about Martha Stewart be without quality merchandise?"</P>
<P>--www.SaveMartha.com</P>

