<P><font face="Times New Roman, Times, serif" size="5"><b>Florida's broken system ruins young lives</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 26, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Last year, you ran an article about the harsh sentence handed out to 12-year-old Lionel Tate, who was charged as an adult with first-degree murder by the state of Florida. I'm writing because right now, there's another similar case in Pensacola, Fla., with 13-year-old Derek King and 12-year-old Alex King standing charged--as adults--with first-degree murder for allegedly killing their father.</P>
<P>But what makes the prosecution of these boys particularly disturbing is the fact that an adult man was recently indicted for being involved in the crime and for sexually assaulting the 12 year old. Despite this, the prosecutor hasn't sent the boys' case back to juvenile court--even though the murdered man's family says that they don't want the boys tried as adults.</P>
<P>Another thing that makes this prosecution appalling is the fact that Father Val Peter, the executive director of Girls and Boys Town in Nebraska, has publicly stated that he is willing to take the brothers into Girls and Boys Town--where they would receive education, counseling, love and support while on probation, at practically no cost to the state of Florida.</P>
<P>Yet despite all these factors, the prosecution of the boys as adults continues. Does that sound like justice to you? It doesn't sound like justice to me. It sounds like a system out of control. </P>
<P>I'm calling on people to contact Florida Gov. Jeb Bush and State Attorney Curtis Golden to back off on this brutal, "get tough" approach and start treating these kids like the children they truly are.</P>
<B><P>Brian Oliver, </B>Maplewood, Mo.</P>
<I><P>Lodge a complaint with the Florida governor's office by calling 850-595-4761.</P>
