<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>How drug companies let this horror happen</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 26, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"IN THE long run, there's no capitalism without conscience," a somber George W. Bush recently declared, in a failed attempt to boost the declining credibility of corporate culture. But even as Bush spoke of conscience, the statistics emerging from the 14th AIDS International Conference in Barcelona demonstrated the absence of it on a global scale. </P>
<P>Of the world's 40 million people infected with HIV, 28.5 million live in sub-Saharan Africa. Average life expectancy is soon expected to fall below 40 years in 10 African countries. In Botswana, the rate of people dying already exceeds those being born.</P>
<P>The mounting death toll from AIDS comes at a time when medical treatment exists in the form of anti-retroviral drugs that can reduce HIV to a chronic illness instead of a death sentence. But just 30,000 of the 28.5 million Africans infected with HIV are receiving anti-retroviral drugs.</P>
<P>The AIDS epidemic worldwide could have been slowed dramatically--as it has been in Brazil, where the government reduced the AIDS death rate by 50 percent by producing its own lower-cost drugs and distributing them to patients for free.</P>
<P>Instead of embracing this strategy, the world's biggest pharmaceutical companies have spent years preventing access to lower-cost AIDS drugs in poor countries, while the rate of HIV infection skyrocketed.</P>
<P>In 1997, 39 drug companies filed a lawsuit after the government of South Africa announced its intention to import or manufacture lower-cost generic AIDS drugs--on the grounds that South Africa was violating the drug companies' "intellectual property rights" and patent protections.</P>
<P>The World Trade Organization grants 20-year patents on most drugs, guaranteeing companies a monopoly without price controls. As a result, pharmaceutical companies are among the world's most profitable corporations. Families USA reported that in 2001, drug company profits--with an 18.5 percent return on revenues--were almost six times higher than the median return for Fortune 500 companies.</P>
<P>According to the India-based generic drug-maker CIPLA, it costs less than $1 a day to produce a three-drug cocktail for a single patient--yet a year's supply costs up to $15,000 in the U.S.</P>
<P>The pharmaceutical companies finally dropped their lawsuit against South Africa only last year--not because they care about the dead and dying, but because of the worldwide outcry against their transparent greed. The end of the lawsuit, however, has barely made a difference for the world's poor.</P>
<P>Adopting a new tactic to stave off the rise of generic drugs, pharmaceutical companies did offer poor countries a limited supply of drugs at costs reduced by up to 70 or 80 percent. But at $1,000 or even $500 per year, the cost is still far out of reach for the populations of countries with per capita incomes of $1 a day.</P>
<P>And so far, few donations have come in to the United Nations Global AIDS Fund, set up to collect money from wealthy nations to help poor countries combat HIV. The U.S., Germany and Japan, three of the world's richest countries, have given just $200 million between them--less than a third of what's needed, and just a tiny fraction of the $1 billion a day the U.S. has spent on its military for decades.</P>
<P>In June, Bush gave another somber speech, this one about AIDS, in which he argued, "In Africa, the disease clouds the future of entire nations"--and pledged "to make $500 million available" to prevent transmission of HIV from mothers to newborns. He didn't mention that this amount, which is spread over three years, amounts to an actual cut from the current level of spending for this type of AIDS prevention.</P>
<P>After the speech, Bush was whisked off to a black-tie fundraising gala that raised roughly $30 million for the Republican Party. The grossly overpaid CEO of GlaxoSmithKline--one of the drug companies that filed the lawsuit against the South African government in 1997--was the most generous of Bush's contributors in the tuxedoed crowd.</P>
<P>There was undoubtedly not a conscience to be found among them.</P>

