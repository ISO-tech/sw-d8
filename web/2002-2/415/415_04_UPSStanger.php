<P><font face="Times New Roman, Times, serif" size="4">UPS harasses activists with repressive rules</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"They violate our dignity"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 26, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>Dawn Stanger, an activist in Teamsters Local 597 in Vermont, was recently fired from United Parcel Service (UPS) for "stealing time"--having taken a minute to run to her car. </P>
<P>In April, UPS instituted rules to control the movements of morning part-time workers. These rules required permission for getting a drink of water and using the bathrooms. As Dawn said, "The rules were unreasonable right from the start, and then were very unevenly enforced."</P>
<P>Once, she was disciplined for getting a drink of water even after having asked--because UPS considered the time looking for a supervisor to ask an "unauthorized break."</P>
<P>"I've been working under these new rules that violate our dignity and right to respect," Dawn said. "It's like they watched me a couple days, and then outlawed any independent movement."</P>
<P>And no wonder. UPS has been waiting for a reason to target Dawn for a while. A highly visible participant in the UPS strike of 1997, she then started a rank-and-file newsletter, UPS Yours, in 1998. She was a union steward from 1997 to 2001, and is also a member of Teamsters for a Democratic Union.</P>
<P>Dawn's being fired is less about "stealing time" and more about leaving the door open to harass union members. "I'm not a thief," Dawn said, "and I don't appreciate being constantly treated like one because I'm pro-union and pissed about unfair new rules." </P>
<P>It's predictable that UPS targeted Dawn--and other Teamster militants--at a time when contract negotiations were uncertain, and the company was fearing another showdown with the rank and file. We have to expose the harassment of workers all around the U.S. and help build a confident rank-and-file fightback.</P>
<B><P>Nate Moore,</B> Burlington, Vt.</P>

