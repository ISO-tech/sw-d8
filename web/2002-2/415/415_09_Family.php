<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Why the family oppresses women</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 26, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE "IDEAL" families portrayed in TV shows like Leave it to Beaver were never close to the truth, even in the 1960s. They're less true today. More than half the U.S. workforce are women. Many families are headed by single parents. In many cases the family acts as a breeding ground for frustration and violence.</P>
<P>The family is the cornerstone of women's continuing oppression in our society. While the relation between men and women has changed immensely over the past few decades, women remain oppressed in our society. "Women have gained the freedom both to have children and to pursue careers," writes author Arlene Skolnick, "but society and institutions have not adapted to a world where women are in the workplace to stay."</P>
<P>Women are still paid less than men, and they still bear the brunt of child rearing and housework. Women are portrayed in various media as sex objects rather than human beings. Abuse, from verbal harassment to domestic violence and rape, is common.</P>
<P>Women's reproductive rights are circumscribed--one product of the backlash of the past few decades against women--through restricted access to health care and abortion, especially for working-class women.</P>
<P>Why are women still oppressed? This question is usually answered in two unsatisfactory ways: male biology (men are stronger) or male psychology (men need to dominate), or a combination of the two. According to this view, held often by both conservatives <I>and</I> many feminists, women's oppression isn't going to disappear as long as men exist.</P>
<P>But there is strong evidence that in hunter-gatherer societies women's subordination to men did not exist. In these societies, there was a sexual division of labor. Men tended to do the jobs that took them further afield: hunting. Women, as child bearers, tended to do the work that kept them nearer to the home base: gathering foodstuffs, preparing hides and clothes, and cooking. But this did not confer lower status on women.</P>
<P>In a society based upon the sharing of goods, women's role was central to the survival of the band. In many reported cases, women exercised ultimate decision-making power, and a woman could divorce her husband simply by placing his belongings outside the home.</P>
<P>The sexual division of labor only took on an oppressive dimension with the rise of class society, when a handful of men became controllers of the agricultural surplus. Women--and most men unlucky enough to be slaves or serfs--became subordinated to a new ruling class. </P>
<P>There isn't the space to trace the history, but the point here is that biology is <I>not</I> destiny. Today, modern technology has rendered physical or biological differences between women and men irrelevant.</P>
<P>Women's oppression persists because of the way in which capitalism benefits from women's <I>unpaid</I> labor in the home. Under capitalism, the tasks of raising the next generation of workers falls to the private family, rather than being taken on by society as a whole. That explains why women's entry into the workforce, the "public sphere," has not resulted in their liberation.</P>
<P>Society still expects women to play their allotted role in the home. Women's liberation from oppression, therefore, is inseparably bound up with the fight to socialize the process of child rearing. Today, it means fighting for equal pay, free and available health care and access to abortion, and fighting for more day-care centers.</P>
<P>But capitalism is unlikely ever to bear the cost of socializing the functions now borne by the family. In the long run, we need a society in which the means of production and reproduction are socialized, and where sex is not bought and sold.</P>
<P>Communal laundries, kitchens and child-care centers would free women from the double burden of work and housework and provide them with the means to become fully free and equal members of society.</P>

