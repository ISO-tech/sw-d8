<P><font face="Times New Roman, Times, serif" size="5"><b>Standing up against Bush's "new crusade"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Glenn Allen</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 26, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>BOOKS:</B> Rahul Mahajan, <B><I>The New Crusade: America's War on Terrorism. </B></I>Monthly Review Press, 2002, 160 pages, $17.95.</P></font>
<font face="Times New Roman, Times, serif" size="3"><I><P>The New Crusade</I> by Rahul Mahajan exposes the greed and hypocrisy behind the U.S. war on terrorism. Mahajan systematically dismantles the lies used to sell the war--from the myth of "surgical strikes" and U.S. humanitarian aid in Afghanistan, to the need to "do something" to stop terrorism.</P>
<P>Mahajan is scathing when dealing with the U.S. media, their self-censorship and complete lack of critical analysis. "Anyone watching the Pakistani press, which is always at risk of overt censorship, pillory their military dictator while papers in the U.S. play softball with Bush has to be appalled," Mahajan writes.</P>
<P>The author is at his best when describing who benefits from and who pays for war. Mahajan contrasts the $15 billion bailout for airline executives and stockholders with the layoffs of 140,000 airline workers. </P>
<P>Most of the book centers on the war in Afghanistan, but the last two sections--which address other U.S. imperialist projects, both military and economic--add historical understanding and theoretical depth. The chapter on Palestine is especially good.</P>
<P>This book is an extremely useful resource, providing needed arguments and historical perspective for antiwar activists.</P></FONT></BODY>

