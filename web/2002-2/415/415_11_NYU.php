<P><font face="Times New Roman, Times, serif" size="5"><b>NYU adjuncts vote union</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Andrew Jagunich</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 26, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--After a two-year unionization drive, adjunct faculty at New York University (NYU) voted to join Adjuncts Come Together-UAW on July 9.</P>
<P>This is only the most recent labor activity at NYU. Last year, graduate teaching assistants won the right to organize, becoming the first group of student workers to have a union contract with a private university.</P>
<P>Adjuncts are part-time professors who make up roughly 70 percent of NYU's 5,600-member teaching force. But they only get paid an average of $2,000-$3,000 for each class they teach, and they receive no health benefits. Many of them have taught at NYU for 10 or 20 years.</P>
<P>"It's supposed to be a place where people think on a higher plane," Martha Bordman, an adjunct in NYU's American Language Institute, told <I>Socialist Worker</I>. But, she said, the university's treatment of workers "shows that NYU is in favor of exploitation."</P>
<P>NYU has responded to the adjuncts' challenge with hypocrisy. Just after the ballots were mailed for the July 9 vote, it announced the creation of as many as 13 full-time positions in the American Language Institute, where there had been only a handful of full-time jobs. These new lecturers would take over most of the work that more than 40 adjuncts now do, putting them out of work.</P>
<P>"It was done to whittle away at the bargaining unit," Bordman said. The union has filed an unfair labor practice complaint with the NLRB. "We're not against full-time jobs, and the union's not against full-time jobs, but we're against the annihilation of adjuncts in entire departments."</P>
<I><P>For more information on the adjunct's struggle, visit the Web site www.nyuactuaw.org.</P>
