<P><font face="Times New Roman, Times, serif" size="5"><b>Thrown in jail for protesting the SOA</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 26, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>A FEDERAL judge in Colombia, Ga., last week sentenced 29 activists to between three and six months in a federal prison and fined them thousands of dollars for the crime of protesting. The activists were among the 10,000 people who took part in protests against the School of the Americas (SOA) in Fort Benning, Ga., last November.</P>
<P>U.S. military officials train their counterparts from Latin America in torture, assassination and other human rights abuses at the SOA. "Those who speak out for justice are facing harsh prison sentences while SOA-trained torturers and assassins are operating with impunity," said Father Roy Bourgeois, a founder of SOA Watch, the group that organizes the annual protests at Fort Benning.</P>
<P>Under growing pressure, the federal government "closed" the SOA in 2000--and reopened it under a new name, the Western Hemisphere Institute for Security Cooperation. But as Maj. Joseph Blair, a former SOA instructor, made clear during testimony, the school's mission is the same. "The courses I reviewed were the same identical courses that I taught at the SOA in the '80s," he said. "They simply changed the names."</P>
<P>Last week's outrageous sentences against protesters, who did no more than commit civil disobedience, is part of the government's long history of trying to silence opposition to the SOA.</P>
<P>But protesters refuse to be pushed back. "The message goes on," said Rev. Jerry Zawada, a 65-year-old Franciscan priest from Cedar Lake, Ind., who was sentenced to six months. "We will close the SOA, or the Western Hemisphere Institute for Security Cooperation, or whatever it is called."</P>

