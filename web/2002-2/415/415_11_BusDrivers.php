<P><font face="Times New Roman, Times, serif" size="4">Mayor attacks strikers</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Bus drivers in NYC stick to their guns</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Michael Watson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 26, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SOME 1,500 bus workers at three private lines in Queens in New York City are entering their sixth week on strike for job security and maintenance of health benefits. The workers, members of Transport Workers Union (TWU) Local 100, are battling New York Mayor Michael Bloomberg as well as company management.</P>
<P>The mayor is using the private lines as a testing ground to beat down expectations for a good contract for 33,000 municipal bus drivers and subway workers--also members of Local 100--whose contract with the Metropolitan Transportation Authority (MTA) expires in December.</P>
<P>The Queens bus workers have been without a contract for 20 months. Some 115,000 commuters have been left without service because of Bloomberg's refusal to fund the rising cost of health benefits and guarantee unions jobs and wages if another company wins a bid to run the routes for the city. Even the bus companies--Jamaica Bus, Queens Surface and Triboro Coach--support the union's demand for job security, so that other companies can't underbid them.</P>
<P>The city recently offered to "loan" the private lines $2 million to pay for increases in health care costs. But that means the city is refusing to pay for the long term--and won't guarantee jobs.</P>
<P>A new offer negotiated by union leaders, management and city officials came at the end of a week of rallies in which bus workers blocked traffic. At a mass meeting to discuss the deal, a group of workers stormed out, saying that union leaders had betrayed them on the issue of job security. This is a legitimate issue.</P>
<P>But Local 100's top official for the private lines, George Jennings, is also exploiting workers' genuine concerns to maneuver inside the local. Jennings is the only member of the conservative old-guard leadership to have survived a humiliating defeat by reformers. He seems willing to risk weakening his own ranks in order to discredit Local 100 President Roger Toussaint--even posing the idea of taking the private bus line workers out of TWU to join another union.</P>
<P>Still, Toussaint himself has come under justified criticism by militants for censoring opponents and violating union democracy. That makes the outcome of the strike even more important--and solidarity is the only way to win.</P>
<P>The work-to-rule campaign at the end of the last MTA contract showed how much strength this union has. Using that power would send Bloomberg a message that he so badly needs to hear--that workers make this city run, not him.</P>
<P>The struggle at the private lines and the fight for a good contract in December are one in the same. It's time to involve all Local 100 workers at the Metropolitan Transportation Authority, both to win the struggle in Queens and to show their strength for their upcoming contract battle.</P>
