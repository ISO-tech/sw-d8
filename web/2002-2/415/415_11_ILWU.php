<P><font face="Times New Roman, Times, serif" size="4">Will union surrender hundreds of clerks' jobs?</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>ILWU debates concessions</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Brian Belknap and Todd Chretien</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 26, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SAN FRANCISCO--Massive contract concessions will be at the center of debate at this week's meetings of West Coast longshore workers.</P>
<P>The meeting of delegates representing the 10,500-strong International Longshore and Warehouse Union (ILWU) dockworkers' division comes a week after union president James Spinosa announced his willingness to accept management's demands to eliminate the jobs of 1,500 clerks. In return, the union wants to represent the far smaller number of computer workers who will replace them.</P>
<P>"It's a concession, clearly," ILWU Local 10 business agent and executive board member Jack Heyman told <I>Socialist Worker</I>. "The ILWU has traditionally said that we're not going to give up our jurisdiction, and this is certainly a step back from that position. This caucus will determine which way the ILWU is going to go."</P>
<P>The employers' Pacific Maritime Association (PMA) is backed by a coalition of manufacturing and retail bosses, all intent on crippling the union's control over how--and how fast--work gets done in the ports.</P>
<P>And under the guise of "the war on terror" Homeland Security Director Tom Ridge has warned the ILWU against a strike as it could disrupt "national security" and Bush administration officials have threatened to use the Taft-Hartley act to declare any strike illegal. Yet ILWU President Spinosa has refused to even hold a strike vote--and has instead agreed to extend the contract day by day.</P>
<P>Unlike the last contract fight in 1999, when the union organized a slowdown, union leaders have stood by as management imposed a speedup on the docks. Shamefully, Spinosa is even packaging retreat as progress. ILWU communications director Steve Stallone called the offer to surrender clerks' jobs a "bold stroke," invoking the name of famous left-wing longshore leader Harry Bridges, who helped to organize the 1934 West Coast general strike that founded the union.</P>
<P>In the 1960s, Bridges agreed to technological changes on the docks that allowed for goods to be shipped in trucking containers. This opened the way for the decline in union membership from 65,000 in 1959 to just 10,500 today. The bosses would like nothing better than to repeat that pattern.</P>
<P>But technology is only partially what this fight is about--the employers want to eliminate the ILWU as a powerful force for workers' rights.</P>
<P>The union can win. But Spinosa will have to be pushed into putting up a fight. "The first order of business should be a strike authorization vote," Heyman said. "The second order of business should be to lift the [self-imposed] gag rule and get information to the rank and file. The third is to enable the rank and file to take job action."</P>
<P>Heyman won a resolution in his local at a membership meeting last Thursday calling on the International to broaden the fight. Turning the tide won't be easy, but if dockworkers decide to fight for their jobs, they won't be alone.</P>
<P>The ILWU has spent the last 15 years building international solidarity with dockworkers from Liverpool, England, to Spain to South Africa to Charleston, South Carolina. Shipping bosses know that a frontal assault on the ILWU could lead to sympathy strikes in ports around the world. That's why West Coast dockworkers have a tremendous amount of power.</P>
<P>If they strike, billions of dollars worth of goods will back up in ports worldwide. And because of their skills, organizing a scabbing operation would be a nightmare.</P>
<P>The ILWU's motto is "An injury to one is an injury to all." Clearly, Spinosa's attempt to abandon the clerks' jobs runs against that principle. Worse, if the bosses and the Bush administration can intimidate the ILWU, it will embolden them to lash out at the entire labor movement.</P>
<P>But a real fight by the ILWU would serve as a crucial rallying point for unions across the U.S. ILWU delegates need to take the initiative and action--and build the solidarity to defend their union.</P>

