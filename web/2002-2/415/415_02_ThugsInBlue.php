<P><font face="Times New Roman, Times, serif" size="4">Videotape of LA cops' assault sparks outrage</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Beaten by the thugs in blue</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Michal Myers</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 26, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>PEOPLE ACROSS the country and around the world were outraged last week by a videotape showing Los Angeles-area police assaulting 16-year-old Donovan Jackson. Jackson, a developmentally disabled African American teen, had committed no crime.</P>
<P>The video shows him handcuffed the entire time, surrounded by cops. Then a white police officer, Jeremy Morse, is seen slamming Jackson's limp body face-first into the hood of a police car, while other cops look on. When the youth turns his head slightly, Morse punches him in the face and begins to choke him. </P>
<P>The images are reminiscent of the vicious beating of Rodney King by LA cops in 1991--which was also caught on videotape and broadcast to the world. This time, the cop assault took place in Inglewood, a Black and Latino community just west of South Central LA.</P>
<P>The first cops on the scene were Los Angeles County sheriffs--whose notoriously racist department was shaken recently by revelations of a white supremacist gang, complete with tattoos and initiation rights, among its officers.</P>
<P>The sheriffs aren't supposed to patrol Inglewood, but two of them pulled into a gas station July 6 and began to question Jackson's father, Coby Chavis, about having expired license plate tags on his car.</P>
<P>When Donovan came out of the station and tried to get into the car, the cops said that they were going to detain him--and then forced him over the hood of the police car when he didn't respond immediately. Donovan has a developmental disability and processes directions more slowly, said a relative.</P>
<P>Four cops from the Inglewood Police Department showed up and began "assisting" in handcuffing Jackson. One officer, Bijan Darvish, admitted to punching Jackson twice in the face. At this point, an unemployed DJ named Mitchell Crooks began recording the scene with his video camera. He captured Morse slamming, punching and choking the completely helpless Jackson--who was later booked on charges of assaulting an officer! </P>
<P>The response of anti-police brutality activists started slowly, but began to gather steam, with dozens of people gathering at the Inglewood Civic Center every day of the following week to demand that Morse be fired.</P>
<P>Tensions grew after Mitchell Crooks was shown on TV being forced into a police car in front of the CNN studios in Hollywood. He was detained on an outstanding warrant from years ago for minor incidents in Northern California.</P>
<P>On July 13, a standing-room-only crowd of 300 packed into the Faith United Methodist Church in South Central LA to hear family members and clergy demand Morse's arrest. The mostly Black audience dug deep to contribute to a defense fund for Crooks, who is white. Meanwhile, Jackson's family filed a federal civil rights lawsuit. </P>
<P>With pressure mounting, Morse and Darvish--who are on paid leave--were indicted. But they could very well go free--like the cops who beat Rodney King.</P>
<P>Everybody from the police to the FBI claims to be investigating the case. But LA had numerous panels and investigations after the 1992 acquittal of the cops who beat King sparked the Los Angeles Rebellion. None of this fundamentally changed the behavior of LA cops--as numerous high-profile scandals since have shown. </P>
<P>The pressure to stop these brutal cops will have to come from below. We need to keep organizing to win justice for Donovan Jackson--and to show these gun-toting thugs in uniforms that we've had enough.</P>

