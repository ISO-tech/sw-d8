<P><font face="Times New Roman, Times, serif" size="4">Feds drag their heels on anthrax suspects</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Homegrown terrorism</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 26, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WHY IS the federal government dragging its feet on the investigation of last fall's anthrax attacks?</P>
<P>Politicians and media "experts" immediately pointed fingers at al-Qaeda and Iraqi "terrorism" when letters laced with anthrax showed up in New York City, Washington, D.C., and Florida. Five people were killed, and 13 were infected. But the Feds' enthusiasm for investigating the attacks seems to have declined as it became clear that they were carried out by a homegrown terrorist.</P>
<P>For months, the FBI has known that the person responsible is almost certainly a government scientist with access to military laboratories. Back in December, tests showed that the type of anthrax used was a highly refined strain of the "Ames" virus--a type of anthrax cultivated by scientists at the U.S. Army's Medical Research Institute for Infectious Diseases (USAMRIID), in Fort Detrick, Md.</P>
<P>Two weeks ago, the prestigious journal <I>Science</I> confirmed that the anthrax likely came out of USAMRIID within the past three years. According to Barbara Hatch Rosenberg, head of the Working Group on Biological Weapons of the Federation of American Scientists, only 20 labs--all in the U.S.--even have samples of Ames anthrax. And of those, only four labs contained the equipment necessary to "weaponize" it into the form that was used in the attacks.</P>
<P>Three of the four labs are U.S. military labs, and the fourth is a government contractor. "The FBI has surely known for several months that the anthrax attack was an inside job," Rosenberg said last February. "According to a former defense scientist, the number of defense scientists with hands-on anthrax experience and the necessary access is smaller, under 50&#133;By now, the FBI must have a good idea of who the perpetrator is."</P>
<P>But it took another couple months for the FBI to investigate the obvious suspects--like a former USAMRIID scientist who apparently commissioned a study into anthrax attacks less than three years ago.</P>
<P>All of this leads you to wonder if the Bush administration wants to cover up the anthrax killer's government ties. This entire case stinks of hypocrisy. The U.S. government's development of the weapons-grade anthrax used last fall is a direct violation of international law and the international Biological Weapons Convention Treaty.</P>
<P>Washington's favorite charge against Iraq's Saddam Hussein is that he wants to build "weapons of mass destruction." But the U.S. is second to none in developing these barbaric weapons.</P>

