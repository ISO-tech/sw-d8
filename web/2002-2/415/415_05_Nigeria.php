<P><font face="Arial, Helvetica, sans-serif" size="2"><b>NIGERIA</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Women's occupation forces big oil to cave</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 26, 2002 | Page 5</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>HUNDREDS OF Nigerian women ended a 10-day occupation of a giant oil terminal last week with a victory over one of the world's most powerful corporations.</P>
<P>ChevronTexaco promised to meet the women's demands after their protest shut down the company's main facility for transporting oil, crippling its lucrative operations in Nigeria. Company officials made an unprecedented promise to build schools, health clinics, town halls, fish and chicken farms, and electricity and water systems in nearby villages.</P>
<P>ChevronTexaco's terminal is located in the Niger Delta--a region rich with oil, yet desperately poor, even compared to other regions of the West African country.</P>
<P>Nigeria's military has long ruled the vast country with an iron fist--though now under the cover of a civilian government elected in 1999. The generals have cut deals with oil multinationals--which then set up operations in the delta that wrecked the environment and brought no development.</P>
<P>Nigeria's oil workers have a tradition of militant struggle, becoming one the main forces opposing military rule. But this month's protests were organized by women, ranging in age from 30 to 90, known in nearby villages as the "mamas."</P>
<P>Their takeover began on July 8 when they stormed a ferry and forced it back to the terminal. The women kept their hold on the terminal by threatening to take off their clothes, traditionally a powerful shaming gesture.</P>
<P>The success inspired similar occupations, with women protesters in control of at least four ChevronTexaco flowstations as <I>Socialist Worker</I> went to press. As one oil worker told a reporter, "The women took Chevron by surprise, and they couldn't believe it."</P>

