<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The bosses' prescription for reform</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 26, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE HEALTH care bosses bought some insurance of their own last month. Two days after the Republicans announced their plan for a Medicare prescription drug benefit for seniors, George W. Bush was the star at a Republican Party fundraiser at the posh Mayflower Hotel in Washington, D.C. Executives from drug giants GlaxoSmithKline, Pfizer and Merck were among the attendees who spent $250,000 a pop to get into the event.</P>
<P>They got what they paid for. The Republican prescription drug benefit proposal amounts to "what Pfizer says, goes." The plan relies on insurance companies--another big campaign contributor--to administer the benefits.</P>
<P>With the debate shifting this week to the Senate, Democrats are preparing to bash Republicans for this health care non-reform. "Watch out, grandma," said Rep. Edward J. Markey (D-Mass.). "GOP now stands for Get Old People."</P>
<P>But despite the rhetoric, the Democrats' proposal offers little relief. Seniors would have to make less than $11,000 a year for their prescriptions to be paid in full. Anyone above that level would have to spend out of pocket until coverage kicks in.</P>
<P>But the Democrats, too, have more important constituents to look after. Last weekend, 16 Democratic senators--including Tom Daschle, Joseph Biden and Hillary Rodham Clinton--flew corporate jets to an invitation-only retreat in Nantucket, Mass. Among the companies providing planes to ferry the Washington gasbags were drug giant Eli Lilly and insurance company AFLAC.</P>
<P>Republicans and Democrats alike complain that a more ambitious drug benefit would bankrupt the Medicare system and force the government to raise taxes. But the money exists to do far more.</P>
<P>According to a Harvard Medical School report published in the journal <I>Health Affairs,</I> the U.S. already spends more than any government in the world on health care--including those with national health insurance. The study looked at spending for government health programs like Medicare, Medicaid and the Veterans Administration--$548.7 billion in 1999--but also took into account tax subsidies for private coverage, which totaled $109.6 billion in 1999. Most of these subsidies go to the wealthiest Americans.</P>
<P>"We pay the world's highest health care taxes, but much of the money is squandered," said study co-author Dr. Steffie Woolhandler. "The wealthy get tax breaks. And HMOs and drug companies pocket billions in profits at the taxpayers' expense. But politicians claim we can't afford universal coverage&#133;We already pay for it, but we don't get it."</P>
<P>We can't let the Republican and Democrats get away with calling the crumbs that they throw us "health care reform." We should call it what it is--an insult.</P>

