<P><font face="Times New Roman, Times, serif" size="4">Why Teamsters should vote "no"</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>What's wrong with UPS deal</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By a UPS worker, Teamsters Local 710</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 26, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE TENTATIVE agreement between the Teamsters and UPS announced last week gives part-timers the shaft--and offers only minimal gains for full-timers.</P>
<P>As <I>Socialist Worker</I> went to press, Teamsters President James Hoffa hadn't released details to members--a full week after announcing the proposed deal. But Hoffa found plenty of time to praise UPS. "UPS is not our enemy," he said. "If there were more companies like UPS, America would be a better place."</P>
<P>Rank-and-file Teamsters feel differently--and this proposed deal is making many angry. One of the most important issues is full-time jobs. UPS depends on a big pool of part-time workers who start at $8.50 an hour and are guaranteed only a few hours a day.</P>
<P>In the last contract battle in 1997, the Teamsters won a 15-day strike that forced management to agree to create 10,000 new full-time positions. Hoffa's agreement this time includes another 10,000 new full-time jobs, but none of them would be created in the first two years of the deal. The result is that the percentage of UPS workers who are part-time will be even higher than the 60 percent level reached before the 1997 strike.</P>
<P>Also, the deal only raises starting pay for part-timers by a miserable 50 cents an hour after 90 days. So new part-timers in 2008 would make $3 an hour less than the $12 an hour they earned before the 1982 contract, when the Teamsters agreed to a wage cut.</P>
<P>Even FedEx, Big Brown's main nonunion competition, pays starting package handlers $11 to $13 an hour. As even <I>Business Week</I> put it in one headline, "UPS Doesn't Deliver for Part-Timers."</P>
<P>For full-time drivers, the deal would increase average pay from about $23 to $29 an hour over six years. Hoffa is trumpeting the full-timers' wage hike as an unprecedented victory. But considering the grueling pace of work and long hours, this simply isn't enough.</P>
<P>And for many over-the-road "feeder" drivers, the issue isn't money, but subcontracting to nonunion trucking companies and railroads.</P>
<P>Meanwhile, by giving UPS a six-year deal, Hoffa undercuts members' ability to fight for a bigger share of Big Brown's profits--which totaled $8 billion over the last five years.</P>
<P>The agreement does say that 10,000 currently nonunion jobs will be made part of the bargaining unit. But this falls far short of the Teamsters' demand for the right to organize all nonunion workers at UPS and its fast-growing nonunion subsidiary, UPS Logistics.</P>
<P>Other weak spots are sure to be found as more details are released. And the new national master contract isn't the last word. There are still about 30 supplements and local riders to be negotiated. These set regional or local issues, like vacation time, pension levels, health benefits and even wages for some job classifications.</P>
<P>Already, the union appears to have made concessions in some local agreements, including a freeze in the big Central States pension fund. Further, two big locals with independent contracts, Chicago's Locals 705 and 710, are still at the table.</P>
<P>UPS Teamsters should vote "no" on Hoffa's weak deal--and mobilize to fight for the contract we deserve.</P>

