<P><font face="Times New Roman, Times, serif" size="4">CEO criminals make workers pay the price</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Robbed by Corporate America</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 26, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>PITY THE poor CEOs. As wave after wave of corporate scandals splash across the front pages, some are feeling a bit tense.</P>
<P>"I'm having a hard time keeping my mind on our business," complained Intel CEO Andrew Grove in the <I>Washington Post</I>. "I feel hunted, suspect--a 'class alien'&#133;I know I'm not alone in feeling this way. Other honest, hard-working and capable business leaders feel similarly demoralized by a political climate that has declared open season on corporate executives and has let the faults, however egregious, of a few taint the public perception of all."</P>
<P>What a shame. Of course, Grove can take comfort in his "modest" salary and bonus of $3 million--not to mention the $143 million he raked in by selling shares of Intel stock. And he's far from the only filthy rich CEO in Corporate America. Ken Lay and Bernie Ebbers are living proof that, even if their companies lurch into bankruptcy amid scandal and accounting fraud, this bunch will do just fine--so long as they can avoid going to jail.</P>
<P>It's a different story for the thousands of workers across the country who are losing their jobs, their savings and their futures because of their bosses' criminal conspiracies. Like the Tyco workers at the AMP electronics plant in Rock Hill, S.C.</P>
<P>When Tyco plunged into the red--mainly because of $27 billion in debt run up by tax cheat CEO Dennis Kozlowski--management decided to slash jobs, announcing plans to lay off a quarter of the 400 employees at the Rock Hill plant and to replace them with low-wage temp workers.</P>
<P>The union workers at Rock Hill fought and won their jobs back. But they know that the corporate crooks will be back for more. "Tyco is so big, they don't even know where Rock Hill is," said one. "They just know the numbers. All we hear is, 'If we don't hit these numbers, we're in trouble.' Are they going to close the plant because we didn't make $90 million last month?"</P>
<P>There's a lot of hot air from Washington politicians about "corporate responsibility." But no one seriously expects these servants of Corporate America to deliver real change. Not with the latest revelations about Harken Energy, showing that the commander-in-thief stole more than just the election--and with his partner in crime Dick Cheney under investigation from his days running the oil services company Halliburton.</P>
<P>The scandal of Corporate America runs deeper than the executives who get caught with their hands in the cookie jar. The truth is that supposedly straight-arrow CEOs like Andrew Grove commit crimes every day--yet they'll never even be indicted, because their crimes are perfectly legal.</P>
<P>Intel's super-profits were extracted through the sweat and blood of workers--whether they endured dangerous conditions and toxic chemicals on the microchip assembly line or put in 60-hour workweeks in office sweatshops.</P>
<P>But that's business as usual for Corporate America. As more of the corporate scum rise to the top, we need to fight for an alternative--one where our needs come before the bosses' profits.</P>

