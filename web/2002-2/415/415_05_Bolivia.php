<P><font face="Arial, Helvetica, sans-serif" size="2"><b>BOLIVIA</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Why is the U.S. threatening Bolivia?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Tom Lewis</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 26, 2002 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>EVO MORALES and the left-wing Movimiento al Socialismo (MAS) won second place in Bolivia's presidential election on June 30. The leader of Bolivia's coca growers (cocaleros), Morales received 21 percent of the vote, compared with 22.5 percent for the leading candidate of the neoliberal Movimiento Nacional Revolucionario (MNR). Morales's impressive showing stunned politicians, both in Bolivia and abroad, because it reflects a leap in social polarization and anti-imperialist feeling.</P>
<P>An opponent of the U.S. government's so-called "war on drugs" and the IMF's privatization program, Morales's leading role in numerous illegal demonstrations and road blockades has earned him the undying hatred of Bolivia's rulers. Last spring, he was stripped of his position as a deputy in the Bolivian congress and accused of sedition. His popularity among Bolivian peasants and workers today signals a broad rejection of U.S. power.</P>
<P>Over the past decade, Bolivia's presidents have been friendly with the U.S. Former military dictator Hugo Banzer and his appointed successor--Harvard MBA and ex-Texas oilman Jorge Quiroga--proved eager to assist the U.S. in implementing corporate globalization and the drug war. Both used violence to impose these ends, but state repression reached a high point during the last two years under Quiroga.</P>
<P>Pre-election polls showed strong support for Morales, setting off alarm bells in Washington. U.S. Ambassador Manuel Rocha went so far as to issue a public statement urging Bolivians not to vote for Morales--and threatened to cut off international aid if they did. "The Bolivian electorate must consider the consequences of choosing leaders somehow connected with drug trafficking and terrorism," Rocha said. Rocha's threat backfired.</P>
<P>After his second-place finish was officially announced--and the election made the MAS the main opposition party in Congress--Assistant Secretary of State for Western Hemisphere Affairs Otto Reich said of Morales, "We do not believe we could have normal relations with someone who espouses these kind of policies." In light of the U.S. role in the recent coup attempt against Venezuelan President Hugo Ch&aacute;vez, Reich's statement had a sinister ring.</P>
<P>It's very unlikely that Morales will win the presidency when the Bolivian Congress--which serves as a kind of electoral college--decides between him and his MNR rival next month. But the much stronger presence of Morales and the MAS will make it harder for the U.S. to dictate economic and social policies. And Morales has vowed to increase the pace of mass direct action.</P>
<P>Morales's party has weaknesses--for example, it supports renegotiation rather than repudiation of Bolivia's debt. But like the defeat of the Venezuelan coup, Morales and the MAS clearly represent a thorn in the side of U.S. imperialism.</P>
<P>And if the revolutionary process in Argentina continues to develop, Morales could move further left.</P>

