<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Ashcroft's plan for ''government-sanctioned Peeping Toms''</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Big Brother wants you</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 26, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>IT SOUNDS like something out of George Orwell's novel <I>1984</I>. But the Bush administration's plan to recruit millions of people as domestic spies--under the so-called Terrorism Information and Prevention System (TIPS)--is all too real.</P>
<P>The pilot phase of the program is due to start in August in 10 cities, with 1 million informants--truck drivers, mail carriers, utility company workers and others--who will be encouraged to report "suspicious and terrorist-related activity." </P>
<P>But don't get the wrong idea. Homeland Security tsar Tom Ridge swears that "the last thing we want is Americans spying on Americans." </P>
<P>How stupid does he think we are? TIPS is a bald-faced attempt to exploit the September 11 tragedy in order to turn millions of people into "government-sanctioned peeping Toms," in the words of an American Civil Liberties Union (ACLU) official.</P>
<P>At the least, TIPS will be an excuse for its spies to act on long-held grudges. And does anyone believe that people of color in general--and Arab immigrants especially--won't be the main targets of the Feds' informants?</P>
<P>The program is so outrageous that a number of major newspapers took an editorial stand against it. U.S. Postal Service officials even announced at first that their employees wouldn't take part--though they backtracked within 24 hours, no doubt after a visit from the White House's political enforcers.</P>
<P>The controversy over TIPS overshadowed the administration's announcement of its overall domestic security proposal last week. One little-noticed point in the proposal: a review of the Posse Comitatus Act, which restricts the military from taking part in domestic law enforcement.</P>
<P>Meanwhile, the Justice Department admitted last week that more than 600 immigrants detained as part of the investigation into September 11 faced secret immigration hearings. "I don't think we had any idea that this number of people had been subjected to closed hearings," said Kary Moss, the ACLU's executive director in Michigan. "If none of these people have been charged with any criminal law violations, then John Ashcroft has essentially, on his own, created two systems of justice in this country."</P>
<P>More than 1,000 people were detained as part of the investigation, but the Justice Department has yet to charge a single one in connection with the hijackings. In other words, Attorney General John Ashcroft's investigation has been nothing more than a witch-hunt against Arabs and Muslims.</P>
<P>But you won't hear much opposition in official Washington--from Republicans or Democrats. No mainstream politician has dared to speak out against Ashcroft's shredding of civil rights. Thus, the Democrats' criticism of TIPS last week focused mostly on how the Feds would be overwhelmed by a blizzard of misleading information--rather than the endorsement of systematic racial profiling.</P>
<P>No wonder Bush and Ashcroft can get away with proposing police-state programs out of George Orwell, and opinion polls show that support for their crackdown, though lower in recent months, remains high.</P>
<P>No one in the political establishment is willing to call them out. That will have to come from below. This is how America's spies and goons were pushed back in the 1960s and '70s. The U.S. government used brutal repression against the civil rights, antiwar and other social movements--spying even on civil rights leaders like Martin Luther King as part of the "fight against communism." </P>
<P>But repression couldn't defeat them. The movements smashed Jim Crow segregation in the South, ended the U.S. slaughter in Vietnam and reshaped U.S. society. They also forced Washington to restrict the previously unlimited powers of the CIA and FBI during the mid-1970s--limitations that Bush's gang is trying to undo a quarter-century later.</P>
<P>All of these movements began with small groups of courageous people who dared to speak out for justice and freedom. That's where we need to start today--by standing up for our rights and exposing Bush and Ashcroft's Big Brother schemes.</P>

