<P><font face="Times New Roman, Times, serif" size="4"><b>McDonald's hits a new low</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CORPORATE GIANT McDonald's has never been known for "good taste"--either in the quality of its food or its ever-present advertising campaigns. But the company hit a new low in August when officials rolled out the new "McAfrika" sandwich at 15 McDonald's restaurants in Oslo, Norway.</P>
<P>According to the company, the sandwich--made of beef and vegetables and wrapped in pita bread--is based on "African recipes," and is a part of the company's attempts to launch more "exotic" food in some of its European chains.</P>
<P>But there's nothing exotic about famine. Right now in Africa, an estimated 12 million people are in danger of starving to death, in one of the worst famines to hit in decades. So to mark the launch of the sandwich, the charity Norwegian Church Aid gathered outside of a McDonald's in Oslo--and handed out the high-protein crackers that aid workers use as emergency rations for starving people.</P>
<P>McDonald's was so embarrassed that it was forced to agree to a deal to inform customers about the potential famine in southern Africa and tell them how they can help.</P>
<P>--Associated Press, August 26, 2002</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Send your kids to Dubya Elementary</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE STOCKTON Unified School Board in California recently voted 4 to 3 to become the first district in the country to name a school after Dubya. Now, small children can have the dubious honor of attending George W. Bush Elementary School.</P>
<P>As one of the four Republican board members told a local newspaper, "I'll be honest: Choosing [Bush's] name is politically motivated, because I am a conservative&#133;It's kind of an 'America now' vote. Through that vote, I was attempting in some way to articulate my patriotism, love of country, deference and respect for the current president and the huge task that faces him."</P>
<P>--DemocraticUndergound.com, June 17, 2002</P>

