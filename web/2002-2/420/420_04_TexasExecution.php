<P><font face="Times New Roman, Times, serif" size="5"><b>Texas' backwards priorities</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 6, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>The state of Texas recently executed Toronto Patterson, the third juvenile offender murdered by the state this summer.</P>
<P>At a recent protest at the governor's mansion, I carried a sign that said "Texas: Number 49 in education spending, number 1 in juvenile executions." I thought to myself: Is there any greater evidence of the completely messed-up priorities of our society?</P>
<P>Well, a new study has revealed just how backwards things are here--between 1985 and 2000, spending on colleges and universities in Texas increased 47 percent, while spending on prisons increased 346 percent. Texas now spends seven times more on prisons than on education and has over 750,000 units in its "corrections system."</P>
<P>People of color have borne the brunt of this racist injustice system. In 2000, there were about 66,000 Black men in state prisons--and only 40,000 in state colleges. This is sickening.</P>
<P>We can't let politicians get away with scapegoating minorities and the poor while they do nothing to improve housing, health care and education.</P>
<B><P>Mike Corwin,</B> Austin, Texas</P>

