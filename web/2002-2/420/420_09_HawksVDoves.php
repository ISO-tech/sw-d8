<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The hawks debate "pre-emptive" war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 6, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THIS SUMMER, the Bush administration and several leaders of the Republican foreign policy establishment have engaged in a strange public dispute over plans for a "pre-emptive" war on Iraq.</P>
<P>In the latest exchange, Vice President Dick Cheney scored one for the ultra-hawks in his blood-curdling Veterans of Foreign Wars speech. "The debate is over," conservative William Kristol told the <I>Boston Globe</I>. "[Cheney's speech] marks a transition from an administration weighing what to do to an administration beginning to make its case at home and abroad over the next two or three weeks in favor of an attack."</P>
<P>For its part, the State Department appeared to distance itself from Cheney's remarks. What is going on?</P>
<P>Certainly one aspect of the constant war talk is Bush's need to keep negative news about the economy, corporate scandals and other subjects unfavorable to him out of the media. Media critic Norman Solomon calls this Bush's "wag the puppy" strategy. With the public backing him on little else, it makes sense for him to keep the war pot boiling through the 2002 congressional elections.</P>
<P>But to see the dispute over Iraq as simply an election-year ploy puts it into a too-narrow focus. If Bush and Cheney were only following a script adviser Karl Rove wrote, it's doubtful that establishment figures like Brent Scowcroft and Henry Kissinger would feel the need to weigh in. At root is a division in the ruling class about how to address what foreign policy experts call "American primacy."</P>
<P>Since the end of the Cold War, the U.S. has had no serious rival to its military power in the world. And even though the 1990s boom fell into recession last year, the U.S.'s unprecedented economic advance pushed it far ahead of most of its economic rivals as well.</P>
<P>That's why Cheney's talk about Saddam Hussein posing a "mortal threat" to the U.S. is such nonsense. The fact remains that the U.S. has no rivals in the world.</P>
<P>And the U.S. government--under Bush and under Clinton before him--wants to keep it that way. The question isn't whether this is the aim of U.S. imperial policy. It's how to go about it.</P>
<P>The "hawks" in the administration, including Cheney and Deputy Defense Secretary Paul Wolfowitz, spelled out their goals long before last September 11 in a 1992 secret Pentagon paper. The paper laid out a vision of a post-Cold War world where the U.S. would aim to "prevent the reemergence of a new rival&#133;This is a dominant consideration underlying the new regional defense strategy and requires that we endeavor to prevent any hostile power from dominating a region whose resources would, under consolidated control, be sufficient to generate global power."</P>
<P>Toward this end, they asserted a U.S. right to intervene anywhere unilaterally: "We will retain the pre-eminent responsibility for addressing selectively those wrongs which threaten not only our interests, but those of our allies and friends."</P>
<P>The hawks had to back down in the face of criticism after the document was leaked to the <I>New York Times</I>. Now the same crew, in power under Bush II, feels it has license to enact a vision considered too controversial in 1992.</P>
<P>The Kissingers and the Scowcrofts don't disagree with the need to maintain U.S. dominance. Nor do they rule out unilateral U.S. action. But they worry that a "pre-emptive" war to enforce a "regime change" in Iraq will backfire. Where the hawks feel that U.S. power alone can make the world bow down to American primacy, the establishment critics think it's preferable to get U.S. allies to "want what we want."</P>
<P>This isn't a debate between "hawks" and "doves." It's a policy dispute between figures who assume that the U.S. is an imperial power and must act like one. And when the Bush administration gives the order for war, you can be sure that all of today's establishment critics will "stand with the president."</P>

