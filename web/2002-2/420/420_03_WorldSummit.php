<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Less than nothing for the poor</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 6, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>PAULA DOBRIANSKY, the U.S. undersecretary of state for global affairs and leader of the American delegation to the Earth summit in South Africa, has some nerve.</P>
<P>"The United States," she lectured reporters in Johannesburg, "is the world's leader in sustainable development." What world is she talking about?</P>
<P>Not the world where global warming is threatening an environmental catastrophe--the Bush administration is "leading" the way in wrecking an international treaty to limit greenhouse gas emissions.</P>
<P>Not the world where two-fifths of the planet don't have access to clean water--the U.S. is "leading" opposition to a resolution to cut that number in half.</P>
<P>Washington's our-way-or-the-highway attitude was on full display at the United Nation's (UN) World Summit on Sustainable Development, held in Johannesburg, South Africa. First, George W. Bush refuses to attend, even after the UN changed the date for him. Then, hacks like Dobriansky throw Washington's weight around to make sure nothing gets accomplished.</P>
<P>"A child dies every 15 seconds for the lack of a toilet, and can you believe that the U.S., Australia and Canada are holding out against an agreement that would halve that number by 2015?" asked Keith Ewing of the British charity Tearfund.</P>
<P>Oh sure, there were announcements from U.S. officials about the aid to the poor that Washington will distribute. But as Philip Clapp of the National Environment Trust said, "The real new money on the table is zero. It's an old American government game. Take money that has already been promised three or four times over, and promise it a fifth time."</P>
<P>What's ironic is the extent to which Washington had already gotten its way before the summit. UN development officials made sure that free-market "solutions" to poverty and pollution would be the centerpiece of discussions.</P>
<P>The hype in Johannesburg is about so-called "type two partnerships" between corporations and non-governmental organizations. But this is just a "greenwash" of some of the world's most hated companies. "[T]hese 'partnerships' have passed into self-parody, with the conference center chock-a-block with displays for BMW 'clean cars' and billboards for De Beers diamonds announcing Water Is Forever," wrote Naomi Klein, a leading voice of the global justice movement.</P>
<P>The Earth summit was designed to be a sham, whether or not George Bush showed up. That's why the most important activities took place outside the conference site--among the tens of thousands of people who gathered to show their opposition to the policies of the corporate globalizers.</P>
<P>In the most militant anti-government protest since the fall of apartheid, 25,000 people marched last weekend from the poverty-stricken Black township of Alexandra to the conference site, in a demonstration organized by South Africa's landless movement.</P>
<P>"We're not backing down," 36-year-old Patrick Mthembu told a reporter. "The people need land. The people need clean water. The people need clean air, and the people need the United States and Europe to play by the same rules of free trade that they force upon poor countries. We will fight their corporate greed with the same fury with which we fought apartheid."</P>
<P>This show of anger against the sick priorities of the free market system holds the real hope for the future, not the Earth summit sham.</P>

