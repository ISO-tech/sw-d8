<P><font face="Times New Roman, Times, serif" size="5"><b>Corporate crime pays well</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 6, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>IT PAYS to cook the books, according to a new report issued by the Institute for Policy Studies and United for a Fair Economy. "CEOs of companies under investigation for accounting irregularities earned 70 percent more from 1999 to 2001 than the average CEO at large companies," the report concluded.</P>
<P>Researchers looked at the top executives of 23 firms under investigation by the Justice Department, Securities and Exchange Commission and other agencies. The suspected crooks raked in an average of $62 million from 1999 to 2001, compared to $36 million for CEOs listed in the annual <I>Business Week</I> magazine executive pay survey.</P>
<P>And workers paid the price. A total of 162,000 workers were laid off at the 23 shady corporations during the same period.</P>

