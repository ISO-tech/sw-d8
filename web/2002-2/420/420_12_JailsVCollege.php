<P><font face="Times New Roman, Times, serif" size="4">The sick face of racism in the U.S.</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>More Black men in jail than college</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 6, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. would rather fill a jail than a school. That was the conclusion of a study released last month by the Washington-based research and advocacy group Justice Policy Institute (JPI). By the end of last year, one out of every 32 adults in the U.S.--or 6.6 million people--was behind bars, on probation or on parole.</P>
<P>The JPI report compared two decades of data from the Bureau of Justice Statistics and the National Center for Education Statistics--and found that while states had plenty to spend on prisons, they had much less to devote to education.</P>
<P>Between 1985 and 2000, the increase in state spending on corrections was nearly double the increase for higher education. "This report underlines the sad reality that the nation's colleges and universities have lost budget battles to the growing prison system," said Vincent Schiraldi, JPI's president and co-author of the study.</P>
<P>Meanwhile, states have shifted the financial burden onto students. Between 1980 and 1998, the cost of paying for tuition at a four-year college rose from 13 percent of a poor family's income to 25 percent. </P>
<P>These statistics are grim, but for Black men, they get even grimmer. African American men are more likely to go to jail than go to college, according to the JPI study. From 1980 to 2000, the number of African American men in jail or prison grew three times as fast as the number in colleges and universities.</P>
<P>Lamont and Lawrence Garrison are two examples. The twins, who are Black, were just a month away from finishing college at Howard University when they were arrested on drug charges in April 1998. They were found guilty based on the unsubstantiated testimony of a confessed drug dealer who was hoping to lower his sentence. Lamont was sentenced to 19 years; Lawrence got 15 years.</P>
<P>This is the price being paid for two decades of the politicians' tough-on-crime policies that have filled prisons, even as violent crime arrests have dropped.</P>
<P>And George W. Bush's law-and-order home state of Texas is the worst. Texas had more adults under correctional supervision than any other state--755,100--last year. From 1986 to 2000, the amount that Texas spent on higher education grew by 47 percent--while its corrections budget jumped by 346 percent!</P>
<P>Both mainstream political parties are to blame for this travesty. Bill Clinton matched the Republicans in punitive crime policies, with truth-in-sentencing and minimum-sentencing laws. "This is because policies that are 'tough on crime' are politically risk free," said Todd Clear, a professor at the John Jay College of Criminal Justice in New York. "They're made by politicians seeking election on the basis of what sounds good at the time, and paid for by people not yet voting. Their effects on the prison population don't come into play until 10 or 15 years down the line."</P>
<P>In some states, the obvious failure of tough sentencing laws has led some legislatures to propose laws to end mandatory minimum sentencing, reform the nation's drug laws and de-fund new prison construction and expansion.</P>
<P>But it's time to look at the whole criminal justice system. There's something wrong with a system that would rather lock away young people than teach them. Education, not incarceration!</P>

