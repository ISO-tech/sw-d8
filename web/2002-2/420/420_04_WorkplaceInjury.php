<P><font face="Times New Roman, Times, serif" size="4">Blaming workers for on-the-job accidents</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Our lives are worth more</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 6, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>Sarah Grey's recent letter (<I>SW</I>, August 9) exposed the real causes of the recent mining disaster in Pennsylvania. The miners' tragedy got a lot of media coverage. However, workplace injuries and deaths are not a rare occurrence in our society. </P>
<P>Here in Greensboro, three construction workers died in August at a construction site. A prefabricated concrete wall collapsed because, according to officials, the steel supports for the wall were taken down prematurely. </P>
<P>Eighty thousand pounds of concrete came crashing down on workers eating lunch in the shade of the building. Six workers managed to get out of the way in time. But three others--like Larry Thompson III, a 20-year-old father of two--were not so lucky. They died on the spot. </P>
<P>The media and local officials are rushing to blame the workers themselves for the wall collapse. But this "accident" could have been prevented if Perry Construction, the general contractor for the project, had conducted safety inspections. </P>
<P>According to the <I>Greensboro News &amp; Record,</I> Occupational Safety and Health Administration officials are not responsible for inspecting the welding that prevents the wall from collapsing. Instead, the construction company itself is supposed to hire "outside experts" to perform these inspections. </P>
<P>But companies are bound to cut corners, putting workers' lives at risk. Perry Construction, for instance, has been cited for safety violations 33 times since 1997 and has been fined a mere $34,000. Is this all that workers' lives are worth?</P>
<P>North Carolina, with the lowest unionization rate in the country, has had 234 workplace deaths since 2000--45 of which were at construction sites. We need a stronger union movement in the South so workers can organize to fight for safer working conditions. </P>
<B><P>Tony Myers,</B> Greensboro, N.C.</P>

