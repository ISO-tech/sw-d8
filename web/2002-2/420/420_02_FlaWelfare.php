<P><font face="Times New Roman, Times, serif" size="5"><b>Florida's sick welfare chief</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 6, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IT WAS clear that changes needed to be made to Florida's child-welfare system earlier this year when 5-year-old Rilya Wilson went missing from foster care for 15 months before anyone noticed. But not the kind of changes that Gov. Jeb Bush has in mind.</P>
<P>The president's baby brother announced last week that he wants to replace the head of Florida's Department of Children and Families (DCF)--with a religious fanatic who advocates beating children. "The Bible is not at all uncertain about the value of discipline," Bush's nominee for DCF secretary, Jerry Regier, wrote back in 1988. "'Although you smite him with the rod, he will not die. Smite him with the rod&#133;save the soul.'"</P>
<P>At the time, Regier was a member of the Christian fundamentalist group Coalition on Revival, which endorses spanking children, even if it causes bruises and welts. In addition, the group advocates making premarital sex and masturbation illegal. It also believes that Christians shouldn't marry non-Christians and that married women shouldn't have careers.</P>
<P>Regier, who worked for George Bush Sr. as head of the National Office of Juvenile Justice and Delinquency Prevention, was named to replace Kathleen Kearney, who resigned after several scandals hit the agency, including Rilya's disappearance.</P>
<P>But more tragedies lie ahead if Bush thinks that "reservoirs of faith" are the answer to a state welfare agency whose real problems are privatization and budget cuts.</P>

