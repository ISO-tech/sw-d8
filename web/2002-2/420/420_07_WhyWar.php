<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Why Washington wanted this war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 6, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>EVERY U.S. military intervention has been accompanied by a web of lies and deception. If political leaders gave their real reasons for going to war, they'd never be able to get any support.</P>
<P>So Washington dresses up its wars with noble-sounding intentions. "The American people have never accepted traditional geopolitics or pure balance-of-power calculations as sufficient reason to expend national treasure or to dispatch American soldiers to foreign lands," admitted Strobe Talbott, a former deputy secretary of state under Bill Clinton. "Throughout [the 20th century], the U.S. government has explained its decisions to send troops 'over there' with some invocation of democracy and its defense."</P>
<P>Thus, George W. Bush claims that the "war on terrorism" will cleanse the world of "evildoers" who "hate freedom" and want to "undermine our democracy." But in reality, the U.S. has very different goals, both short and long term, that it wants to accomplish with the "war on terrorism." These goals have nothing to do with September 11--and in fact existed before the attacks.</P>
<P>Washington's first aim, which it has already accomplished, was the removal of Afghanistan's Taliban regime. When the Taliban took power in 1996, the U.S. supported the Islamist hard-liners, figuring that the Taliban could be persuaded to help the U.S. secure influence in the region. "This amoral or immoral policy is based on the assumption that the Taliban would bring stability to Afghanistan and permit the building of oil pipelines from Central Asia through Afghanistan to Pakistan," conservative Rep. Dana Rohrabacher (R-Calif.) said at the time.</P>
<P>Indeed, the U.S. attitude had to do with the discovery of huge oil and gas reserves in the Caspian Sea region northwest of Afghanistan, which touched off a scramble for control. But as it turned out, the Taliban didn't readily bend to U.S. demands--so Washington turned to military measures to demonstrate its seriousness.</P>
<P>In 1997, the U.S. deployed paratroopers to the former Soviet republic of Kazakhstan in the longest airborne military exercise in history--and then held a press conference to make sure that the whole world got the picture. "The message is that there is no nation on the face of the Earth that we cannot get to," said U.S. Marine Gen. John Sheehan.</P>
<P>But intimidating the Taliban and pursuing oil weren't the only interests involved. The collapse of the USSR in 1991 left a power vacuum in Central Asia and sparked a revival of what 19th-century diplomats called the "great game"--military and political maneuvers by the big powers to gain control of the region. The U.S. has been seeking to increase its geopolitical influence, in competition with Russia, China and India. Now, it has the excuse to set up its own military bases in Central Asia.</P>
<P>But the "war on terrorism" also has its contradictions. In the Middle East, Israeli Prime Minister Ariel Sharon has used Bush's "war on terror" rhetoric as an excuse for stepping up the savage suppression of Palestinian resistance. This has stoked anger against the U.S.--and helped pressure Arab regimes that backed the first U.S. war against Iraq to oppose a new assault.</P>
<P>For socialists, the task is to help build an antiwar movement that can expose the U.S. government's real war aims--and show the politicians that we won't stand by while they order murder in our name.</P>
<P>Initially, antiwar activists may be isolated. But it's important to remember that the anti-Vietnam War movement was for years confined to small numbers. Many people believed the politicians who said that the U.S. was "defending democracy." But growing numbers of people came to reject this lie--and ultimately took action to help defeat U.S. imperialism.</P>

