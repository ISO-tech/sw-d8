<P><font face="Times New Roman, Times, serif" size="4">Ex-Union Carbide CEO enjoys retirement</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Why isn't he on trial?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 6, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WARREN ANDERSON should be on trial for mass murder. Instead, he's enjoying retirement in his multimillion-dollar mansion in the Hamptons on New York's Long Island.</P>
<P>Anderson was Chief Executive Officer of Union Carbide (now part of Dow Chemical) in December 1984 when an explosion at the company's plant in Bhopal, India, released a cloud of lethal gas in the worst industrial disaster in history. Safety equipment that could have prevented the leak wasn't working, and gas storage tanks had been overfilled.</P>
<P>Three thousand people were killed that day, and another 10,000 have died since as a result of the disaster. More than half a million people were seriously injured.</P>
<P>And Union Carbide's response? Pay survivors less than $550 each--then abandon the plant, leaving behind tons of toxic chemicals and contaminated groundwater for anyone living around the site.</P>
<P>Since 1992, Anderson--who retired in 1986--has faced charges of "culpable homicide" in India. But the Indian government has never sought his extradition, claiming that it didn't know his address. Last week, a member of Greenpeace took care of that problem--by showing up on Anderson's doorstep to present a copy of the arrest warrant.</P>
<P>Still, facing pressure from the U.S. government, prosecutors in India don't seem very interested in pursuing the case. They even tried to get a court to reduce the charges against Anderson to causing "hurt by negligence."</P>
<P>According to Satinath Sarangi, a member of activist organization Bhopal Group for Information and Action, lessening the charges would have been the equivalent of "converting the world's worst industrial disaster into a car accident." But Indian judges rejected the motion.</P>
<P>Getting Anderson into court will only happen if the U.S. extradites him to India--something that is unlikely as long as authorities claim that his whereabouts are "unknown."</P>
<P>Yet Greenpeace activists found him easily enough. As activist Casey Harrell said, "If Greenpeace can track down India's most wanted, why haven't the U.S. authorities extradited him?"</P>

