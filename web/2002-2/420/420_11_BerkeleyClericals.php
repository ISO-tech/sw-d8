<P><font face="Times New Roman, Times, serif" size="5"><b>Clericals strike at Berkeley</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>FOR THREE days last week, the Coalition of University Employees (CUE) at the University of California-Berkeley walked the picket line. "The issue in this strike is [the university's] unwillingness to bargain fairly and abide by the law," explains Margy Wilkinson, chief negotiator for the clerical workers. </P>
<P>The union--which represents 1,900 clerical workers at Berkeley--timed its strike to coincide with the beginning of the school year and organized boisterous pickets at entrances across campus. Workers are seeking better wages and working conditions--and have filed several unfair labor practice suits against the university. After a mere 3.5 percent raise over the last two years, CUE is demanding a 15 percent raise in the next two years.</P>
<P>About 2,500 graduate teaching assistants in United Auto Workers Local 2865 refused to cross CUE's picket lines, and on Wednesday about 600 lecturers on campus joined the walkout. Many classes were canceled.</P>
<P>The university is pleading poverty because of budget cuts, but officials conveniently neglect to mention their $2 billion reserve that could easily be used to provide better pay and conditions. "There is money for the top jobs, but not for the clerical workers who keep things running smoothly," said striker Susan Peabody.</P>
<P>Workers at other University of California campuses also covered by the CUE contract are watching the Berkeley strike closely. Last week, clerical workers at UC San Francisco also authorized a strike.</P>
<P>And at UC Santa Cruz, CUE workers and supporters held three days of solidarity rallies, culminating in a March for Respect of more than 100--a good turnout considering school is still in recess there.</P>
<P>Members of at least six labor organizations, city council members and student government representatives all pledged their solidarity to the Berkeley strikers--as well as to honor picket lines when the Santa Cruz clericals walk out.</P>
<P>The strikers ended their walkout with two rallies--one at Chancellor Robert Berdahl's office and the other at UC President Richard Atkinson's office in Oakland. "We're hoping this will show them how far we're willing to go and how serious we are," said Yakov Wiegmann, a striking library assistant.</P>

