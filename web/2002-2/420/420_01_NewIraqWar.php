<P><font face="Times New Roman, Times, serif" size="4">Planning new war on Iraq</font><BR>
<font face="Times New Roman, Times, serif" size="4">Witch-hunting Arabs at home</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Tell Bush: We say no!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 6, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>AS THE anniversary of the September 11 attacks approaches, the Bush administration is planning somber memorials to the victims.</P>
<P>But there won't be any memorials for the thousands of innocent Afghan civilians killed by U.S. bombs. Nor will anyone mention the hundreds of young men thrown in U.S. jails or deported for no other reason than that they were Arab or Muslim.</P>
<P>What there will be is a calculated, cynical effort by the Bush gang to build more pressure for the next phase of their "war on terror"--a war on Iraq that will cause unspeakable destruction and bloodshed. It's no coincidence that the Justice Department chose last week to announce indictments against six men for "giving material support or resources" to terrorists.</P>
<P>Who are these men? One is Farouk Ali-Haimoud--who worked at an ice cream shop in the Detroit airport. He was arrested last fall, but prosecutors couldn't find any evidence against him. But that didn't stop the Justice Department. "He's 21 years old, and he worked as a bus boy and then at Edy's ice cream, and he's supposed to be the mastermind of a jihad?" asked Kevin Ernst, Ali-Haimoud's lawyer.</P>
<P>But while federal officials crowed about the indictments, there was barely a mention of the Florida doctor who was recently caught plotting to blow up dozens of mosques. For the U.S., Arab lives just don't count as much. That's clear from the latest push for a new war against Iraq.</P>
<P>The 1991 Gulf War left as many as 200,000 Iraqis dead in a matter of a month and a half--and a decade of sanctions has killed 1 million more. But the Bush administration won't be happy until it can "go back and finish the job." Vice President Dick Cheney made that obvious every time he approached a podium last week--to rant about the need for a "regime change" in Iraq, no matter what the cost.</P>
<P>Leading voices of the Washington establishment may disagree about when and how--but most Republicans and Democrats are united on war. That sets them apart from the growing number of people who question Bush's war drive. A recent <I>Time</I> magazine/ CNN poll found that support for an invasion of Iraq has dropped to just 51 percent of respondents, down from 70 percent last fall.</P>
<P>Activists have planned more than 100 vigils and antiwar protests in some 36 states for the week of September 11. The rallying cry of the antiwar movement a year ago was "Don't turn tragedy into war." That will be just as relevant in the coming weeks, as Bush and Co. try to exploit September 11 again to build pressure for invading Iraq.</P>
<P>It's time to tell Bush: No to war and no to racism!</P>

