<P><font face="Times New Roman, Times, serif" size="5"><b>Groceries meant more than her life to police</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 6, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>The self-proclaimed judge, jury, and executioners of Marcella Byrd defended their actions to the public last month in Long Beach, Calif.: "It was a split second decision to counter the threat to the community," claimed a tearful police chief whose emotional display did not even rise to the compassion of a crocodile. </P>
<P>Releasing the report on the police killing of Marcella Byrd at an NAACP meeting, the chief shed his tears not for the victim--a 57-year-old mentally ill African American woman--but for the police officers who pulled the trigger. "That action will make indelible impressions on them," said the chief.</P>
<P>Many in the audience were quick to raise the issue of police training, and draw the connection to racism rampant in the criminal "justice" system. The police chief's response? "They were only looking at her as an armed suspect who attempted to steal a cart of groceries," he said. "They were not looking at the color of her skin." </P>
<P>Cops are there to "serve and protect" a system in which someone like Marcella has to steal a cart of groceries to survive--a system in which a person's right to life is trumped by the right to profit. For this, seven armed officers claimed to be so afraid of a 57-year-old woman with a knife that they had to take her life! </P>
<P>One piece of information the public has heard little about is the fact that two of the seven officers had "non-lethal" bean-bag guns, yet chose to shoot with live ammunition. This makes it brutally clear that the Long Beach police place no value on the life of a poor African American woman.</P>
<B><P>Cesar Montufar,</B> Long Beach, Calif.</P>

