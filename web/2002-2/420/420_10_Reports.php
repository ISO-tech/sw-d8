<P><font face="Times New Roman, Times, serif" size="4"><b>Protest George W. Bush</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Adam Meyer and Mike Corwin</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CRAWFORD, Texas--Even at his summer home, George W. Bush can't escape protesters opposing his imperialist war drive. On August 24, 300 activists from across the state gathered in Crawford to speak out against Bush's plan for war against Iraq.</P>
<P>At a rally, speakers talked about the horrific nature of the current U.S. policy towards the people of Iraq and outlined ways to oppose the new plans for war. Many expressed optimism at the growing questioning that is leading toward active opposition to the Bush regime. "We're the only ones who can stop them," one demonstrator told <I>Socialist Worker</I>.</P>
<P>After the rally, demonstrators caravanned to Bush's ranch. Finding the entrance blocked by police, the caravan pulled over, and protesters gathered to present an antiwar petition to be delivered to Bush. Activists left the demonstration happy that we showed Bush that he can't hide.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Fight for affordable housing</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paige Sarlin</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--About 100 people gathered in front of the former Roosevelt Hotel August 26 to protest the housing crisis here.</P>
<P>Like many cities, Chicago's poor and low-income workers have found it harder and harder to find affordable places to live, as developers snatch up property to build luxury lofts. There is a citywide shortage of more than 150,000 affordable rental units. And there are 52,000 eligible families on the Chicago Housing Authority's (CHA) waiting list.</P>
<P>Despite this, the CHA's "Plan for Transformation" calls for more than 18,000 units of affordable housing to be demolished. Some 6,000 families will have to find housing in the private market--where rent is so high that a worker making minimum wage would have to work 110 hours a week to be able to afford a two-bedroom apartment. </P>
<P>The march, organized by the Coalition for Fair Community Development, went past a number of single room occupancy hotels and missions that provide low-cost housing--and which have come under attack as the city, led by greedy developers, tries to clear all the areas near the Loop for the rich. </P>
<P>The march ended at Millennium Park, where a tent city was built to dramatize the fact that the $370 million used to build the park could have created close to 2,000 units of affordable housing. Protesters showed that we won't stand by while the city puts profits before needs.</P>

