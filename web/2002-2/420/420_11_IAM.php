<P><font face="Times New Roman, Times, serif" size="4">Mechanics at United organize against concessions</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"Full pay to the last day"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jennifer Salazar Biddle, shop steward, IAM Local 1781</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 6, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SAN FRANCISCO--Workers at United Airlines (UAL)--as well as those at Boeing and US Airways--have suddenly found themselves in the eye of the storm. Corporate America is making a frontal attack on workers in a bid to completely break the back of organized labor in this country.</P>
<P>The International Association of Machinists (IAM)--which represents most employees at these three companies--is one of the largest and most important transportation unions in the U.S. The aviation industry is the most densely unionized sector of the American economy, with roughly 80 percent of its workers unionized.</P>
<P>On August 28, UAL presented its unions with a concessionary contract proposal that is nothing short of union busting. Entitled "Recovery Program Term Sheet," it represents $9 billion in concessions over six years. For mechanics, it includes an immediate 10.4 percent wage reduction of our gross pay with 1.5 percent increases each year and massive cuts in health coverage.</P>
<P>Over the past decade, UAL has drastically increased its outsourcing of maintenance to nonunion, low-wage facilities. UAL says it's outsourcing 11 percent of our work--well below the 20 percent cap in our contract. But with overhaul maintenance crews at times standing idle, many mechanics believe the company has far exceeded the limit already. Unfortunately, we have no mechanism in our contract to make the company prove it has not violated the limits.</P>
<P>Ostensibly, the airlines want concessions in response to the economic downturn and the impact that September 11 has had on the industry in the past year. United set a September 16 deadline for unions to agree to their "Recovery Program," or the company says it will go bankrupt.</P>
<P>But the numbers don't add up. If, as the company maintains, it is losing $1 million a day, then why does it need more than four times that loss in concessions from labor?</P>
<P>Employees are furious. Locked into a concessionary contract during the boom years of the 1990s, they sacrificed while the company was fat with cash and only recently achieved parity with other airline workers.</P>
<P>The IAM's response has been atrocious. First of all, the union has supported UAL's bid for government loans--knowing that such loans require concessions. Then, when UAL's CEO Jack Creighton started talking about concessions and layoffs several months ago, the union insisted workers would simply vote concessions down and take layoffs--as if layoffs were the better alternative.</P>
<P>Now that the company is threatening bankruptcy, IAM officials are saying the "Recovery Proposal" would be better than dealing with a bankruptcy judge.</P>
<P>While our union appears clueless, this moment has not been lost on workers at UAL. "We are surely living in times that will redefine labor and in turn America," wrote one mechanic. The lesson we are learning now is that in this climate of a "war on terrorism," workers won't hold onto any of the things we take for granted without a fight.</P>
<P>And a fight is possible. According to the IAM's attorneys, no precedent has been set for workers striking a bankrupt airline. The general feeling among mechanics is, "If we can't have it, then greedy executives can't have it either." Many are saying we should strike if our contracts are voided in bankruptcy.</P>
<P>Everyone I know is saying, "Full Pay to the Last Day!"</P>

