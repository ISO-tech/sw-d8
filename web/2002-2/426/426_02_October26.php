<P><font face="Times New Roman, Times, serif" size="4">Say no to Bush's war on Iraq</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Getting the word out about October 26</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 18, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MARK THE date on your calendar--October 26. That's when we'll protest George W. Bush's war on Iraq from one end of the country to the other.</P>
<P>Activists across the U.S. are building for national protests, called by the group ANSWER, in Washington, D.C., and San Francisco. At dozens of campuses, students are working hard to get out the word.</P>
<P>In New York City, the Anti-War Coalition at Hunter College has booked four buses for D.C. and is holding tabling and a speak-out to make sure that they're filled.</P>
<P>On October 10, six antiwar activists took signed petitions to the New York City offices of Sen. Hillary Rodham Clinton, demanding that she vote "no" on the congressional resolution authorizing a war. When Clinton refused to speak with them, they organized an eight-hour sit-in.</P>
<P>In Chicago, a teach-in at the University of Illinois-Chicago, featuring the anti-sanctions group Voices in the Wilderness, attracted 45. At the University of California (UC)-Berkeley, 40 people came to see the film <I>Hidden Wars of Desert Storm, </I>followed by discussion led by a former Iraqi citizen who fought in the anti-Saddam rebellion after the war that was put down with the help of the U.S.</P>
<P>Afterwards, activists planned outreach for the October 26 protest. The University of Washington Coalition Against the War is making plans for a send-off rally for vans going to the October 26 protest in San Francisco.</P>
<P>White House Press Secretary Ari Fleischer got the unwelcome he deserved last week when he went to Middlebury College in Vermont to receive an alumni achievement award. Some 1,500 people turned out to protest Bush's mouthpiece. "We were expecting maybe 500, which I thought was optimistic," said Ben Gore, a college student in the local group, United for Peace. "This is beyond belief."</P>
<P>UC-Santa Cruz's antiwar group, Standing United for Peace Coalition, called a student walkout on October 7--to mark the one-year anniversary of the start of bombing on Afghanistan--and more than 600 students, staff and faculty came out.</P>
<P>In Chapel Hill, N.C., 325 people took part in a protest outside of Democratic Rep. David Price's offices--and 10 staged a 26-hour sit-in. Last week, Iowa City was alive with antiwar activity, including three rallies and a speech by antiwar activist Rania Masri.</P>
<P>Lise Vandervoort, president of United Electrical (UE) Workers Local 896, which represents graduate employees, spoke at one rally. At its national convention, UE unanimously passed a resolution opposing the use of force in Iraq.</P>
<P>In Seattle, 4,000 people attended a silent antiwar march sponsored by the Church Council of Greater Seattle. And in Rochester, N.Y., more than 700 protested on October 7.</P>
<P>In Olympia, Wash., where about 500 people rallied on October 7, local college professor Larry Mosqueba told the crowd, "If Bush wants to stop terrorism, the first thing he should do is go to Maine and arrest his father."</P>
<I><P>Candice Amich, Annette Bauer, Brian Erway, Brian Huseby, Steve Leigh, Margaret Loose, Afsaneh Moradian, Sid Patel and Brad Ward contributed to this report.</P>
