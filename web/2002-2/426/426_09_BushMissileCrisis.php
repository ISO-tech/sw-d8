<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Bush gets his own "missile crisis"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 18, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SINCE THE Bush administration announced its new doctrine of "preemption"--invading any country it chooses--its claim has been that they aren't advocating anything new. After all, they say, they're merely following in the footsteps of President John F. Kennedy during the 1962 Cuban Missile Crisis.</P>
<P>"As President Kennedy said in October of 1962, 'Neither the U.S. nor the world community&#133;can tolerate deliberate deception and offensive threats on the part of any nation, large or small,'" said Bush in his speech last week. "'We no longer live in a world,' he said, 'where only the actual firing of weapons represents a sufficient challenge to a nation's security to constitute maximum peril.'"</P>
<P>In 1962, the world learned of the peril when the U.S. publicized spy photos that showed that the USSR was building nuclear missile bases in Cuba. The U.S. set up a naval blockade around the island, threatening to seize or attack Russian ships carrying missile parts to Cuba. The missile crisis ended when the Russians backed down, agreeing to withdraw the missiles from Cuba in exchange for U.S. withdrawal of obsolete missiles in Turkey.</P>
<P>Though the U.S. "won" the missile crisis, it brought the world the closest it's come to global nuclear war. For a few days in October 1962, millions across the U.S. literally thought they might be living their last days.</P>
<P>The Cuban Missile Crisis has many parallels with Bush's current plans to invade Iraq--but not the reasons that Bush and his advisers tout. Start with the U.S. government's stated goals in 1962 and today. Bush says he's concerned about enforcing United Nations resolutions and destroying Saddam's "weapons of mass destruction." But his real reasons for invading Iraq lie elsewhere--in establishing the U.S. as boss of a global empire and in grabbing control over the Middle East's oil.</P>
<P>JFK said he was dedicated to protecting the Western hemisphere from nuclear war. Yet in all their deliberations over Cuba, JFK and his advisers voiced two overriding worries, according to secret White House tapes published in Ernest May's and Philip Zelikow's <I>The Kennedy Tapes </I>in 1997.</P>
<P>One was that the USSR would gain an advantage over the U.S. in Europe if the U.S. didn't force the Russians to back down. Their second worry was that Latin American countries would feel bolder about carrying out a foreign policy independent of Washington's.</P>
<P>In other words, the reasons Kennedy provoked the missile crisis had nothing to do with his publicly stated aims. The U.S. was willing to contemplate the incineration of millions to protect American "credibility."</P>
<P>Another echo of the past is Bush's fanatical pursuit of "regime change" against U.S.-fingered bogeymen. Saddam Hussein is a former CIA "asset" who became a target for "regime change" after he fell out with Washington in the early 1990s.</P>
<P>In the early 1960s, Fidel Castro's new government--the result of a revolution against a U.S.-backed dictator--represented a finger in the eye to the U.S. From the start, the U.S. looked for ways to undermine the Castro government. In fact, the missile crisis followed directly from the failure of the CIA-sponsored Bay of Pigs invasion of Cuban exiles in 1961. Facing everyday threats from one Cold War superpower, Castro turned to the other superpower--the USSR--for protection.</P>
<P>Secret White House tapes revealed Kennedy planning a "first strike" invasion of Cuba and casually discussing occupation with Britain's prime minister. In 1962, Kennedy learned in a top-secret briefing that as many as 50 million Americans could be incinerated if the USSR launched a nuclear strike. CIA chief John McCone concluded that not much could be done to save them. Kennedy decided to conceal this from the U.S. public.</P>
<P>Today, Bush is fully aware of the dire results that his plan to invade Iraq will produce. And you can be just as sure that he's hiding them from the world. </P>

