<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">White House pushing for years of direct military rule of Iraq</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Washington's plan for an oil colony</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 18, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH administration plans to install a new dictator in Iraq. Only this one will be an American.</P>
<P>According to press reports last week, the White House is preparing for years of direct U.S. military rule over Iraq after a new assault to topple Saddam Hussein. "Our intent is not conquest and occupation of Iraq," top Bush adviser Zalmay Khalilzad said in a recent speech. "But we do what needs to be done to achieve the disarmament mission and to get Iraq ready for a democratic transition."</P>
<P>And "what needs to be done," as far as the White House is concerned, is for Gen. Tommy Franks, the commander of U.S. forces in the Gulf, to become the military ruler of Iraq. So much for that talk about "liberation." The Bush gang is ready to turn Iraq into a U.S. colony--with Franks as the overlord.</P>
<P>U.S. officials admit that they want to move away from the "Afghanistan model"--where Washington installed a puppet government that has been plagued by bloody infighting between rival warlords.</P>
<P>The reason for the shift can be summed up in one word: oil. With Iraq possessing the second-largest oil reserves in the world, Washington doesn't want to risk any instability in a post-Saddam settlement.</P>
<P>If any more evidence was needed, the plan for military rule certainly gives the lie to Bush's claim that he hasn't made up his mind about a new war on Iraq--a popular lie during last week's debate in Congress on a resolution authorizing military action. Any Democrats who hid behind the illusion that they were voting to strengthen United Nations weapons inspections should admit the truth--that they wrote a blank check for war.</P>
<P>Most people in the growing antiwar movement recognized the vote in Congress for what it was--a green light for the so-called Bush Doctrine, under which the White House claims the right to intervene around the world in "pre-emptive" wars.</P>
<P>Unfortunately, some antiwar voices don't recognize the full implications of this. For example, a few activists want the antiwar movement to keep quiet about last year's U.S. war on Afghanistan. In an article in the <I>Nation</I> magazine, liberal journalist Liza Featherstone complained that "[e]ven the smartest groups are making some questionable decisions, continually harping on the U.S. intervention in Afghanistan, a fait accompli that was enthusiastically supported by most Americans."</P>
<P>It is true that some people who have taken action against the Iraq war drive were either unsure about the U.S. bombing of Afghanistan or supported it. But the antiwar movement shouldn't be shy about showing why the two are connected. The Bush gang certainly linked them all along--with Afghanistan serving as a stepping stone for the war on Iraq, which in turn would pave the way for further "wars on terror."</P>
<P>This is an argument that we can win. The more that people learn about the disastrous situation in postwar Afghanistan and the Bush gang's Iraq war drive, the more obvious it is that talk about democracy and justice is a lie in both cases. These are two phases of a single imperialist war by Washington to ensure its position as global super-cop.</P>
<P>But Washington's arrogance has sparked an angry reaction across the world. And more and more people in the U.S. are realizing that this isn't the kind of future that they want either. Opinion polls show a new tide of questioning of the war, and activism has grown faster than many organizers expected--with as many as 100,000 people taking to the streets in dozens of cities around the U.S. on the weekend of October 6.</P>
<P>Now, the antiwar movement must turn to October 26--and build the largest possible turnout for protests in Washington, D.C., and San Francisco. We can send a message, loud and clear, to the Bush gang that we don't want a new slaughter in Iraq--or their imperialist plans for expanding U.S. power around the globe.</P>

