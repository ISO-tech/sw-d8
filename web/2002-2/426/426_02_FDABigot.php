<P><font face="Times New Roman, Times, serif" size="5"><b>The administration's anti-woman nut</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 18, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>W. DAVID HAGER may soon become the chair of the Food and Drug Administration's (FDA) panel on women's health policy. Only problem is, Hager doesn't have a very high opinion of women.</P>
<P>In addition to being an obstetrician-gynecologist, Hager is a right-wing Christian fundamentalist--and his religious affiliation seems to have contributed to his ideas about women's health more than his medical knowledge.</P>
<P>Along with his wife, Hager authored <I>Stress and the Woman's Body,</I> which suggests that women to use Scripture readings to cure their physical ailments. For headaches, Hager recommends a verse from Matthew 13:44-46, and for premenstrual syndrome, he suggests Romans 5:1-11, "Tribulation worketh patience."</P>
<P>Hager also believes that women today should thank Jesus for their liberation. "Jesus stood up for women at a time when women were second-class citizens," he wrote. "I often say, if you are liberated, a woman's libber, you can thank Jesus for that."</P>
<P>Naturally, Hager is an anti-abortion nut. He has lobbied the FDA to reverse its approval of the abortion pill RU-486. And Hager says that he "prefers" to not dispense contraceptives to single women, instead urging abstinence. Hager is also the editor of a book that contains a chapter--which he now says he disagrees with--called "Using the Birth Control Pill is Ethically Unacceptable."</P>
<P>Hager was chosen for the post by FDA Senior Associate Commissioner Linda Arey Skladany--a former drug industry lobbyist with ties to the Bush family.</P>
<P>FDA advisory panels often have a big say over major questions of health policy. If the Bush administration succeeds in getting Hager appointed to a four-year term, he will lead its study of hormone-replacement therapy for menopausal women, one of the biggest controversies in health care today. </P>
<P>The idea that this bigot could be put in charge of a panel that oversees new developments in women's health care is infuriating. We can't let the Bush administration roll back the clock on women's health.</P>

