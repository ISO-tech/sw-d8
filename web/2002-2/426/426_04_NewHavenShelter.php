<P><font face="Times New Roman, Times, serif" size="4">New Haven closes a shelter on September 11</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Kicked out in the street</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 18, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>How did the city of New Haven, Conn., honor the anniversary of September 11? They kicked 75 people out of a homeless shelter!</P>
<P>On September 11, the city shut down its emergency overflow shelter because they claimed that they didn't have enough funding to keep it open--even though there are million-dollar plans in the works to revamp the downtown mall and tear down a stadium. </P>
<P>They slated the closure for September 11 for the most sinister of reasons. They knew the story would not be covered widely in the media because of all the patriotic fervor surrounding the anniversary.</P>
<P>But the people who were left with nowhere to sleep surely didn't feel very patriotic. "This country is unfair&#133;You're always going to have terrorism as long as you're unfair," said one man who was kicked out of the shelter. </P>
<P>A few months prior, he had been forced to sell his home to pay off back taxes. He's been homeless ever since. "There is very little housing available for people working a minimum wage in this city&#133;Hard times are nothing new," he said.</P>
<P>Respect Line, a group made up of homeless citizens and students, has set up a temporary tent village, which a local church is housing on its section of the public green. Community groups have been supplying everything from tents and blankets to food, but there is only so much that they can do. </P>
<P>The city is saying they can't open another shelter until November. That will mean nearly two months of camping outside, right as the weather begins to turn cold. There is no heat and no access to running water. And some of the campers are pregnant women.</P>
<P>It's ridiculous that in a country that pours more than $1 billion a day into war efforts, we can't afford a basic necessity like shelter for all our citizens. But these are exactly the kind of circumstances this sick system produces. That is why we need to attack the problem of homelessness at its root, which is capitalism.</P>
<B><P>Leela Yellesetty,</B> New Haven, Conn.</P>

