<P><font face="Times New Roman, Times, serif" size="4">Why we're marching</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Stop Bush's war on Iraq</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 18, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH and his gang will say anything to justify their war on the world. No lie is too outrageous to tell. That was clear from Bush's prime-time speech last week making the case for an invasion of Iraq.</P>
<P>Bush talked about Iraq's "weapons of mass destruction." But he didn't talk about the experts who say that Iraq no longer has the capability to produce those weapons. He paid lip service to the suffering of ordinary Iraqis--but said not a word about the more than 1 million people who have died because of U.S.-sponsored economic sanctions over the past decade.</P>
<P>Likewise, no tragedy is too awful for the White House to exploit as a justification for extending their "war on terror." The administration wasted no time in labeling this week's horrific bombing of a tourist nightclub on the Indonesian island of Bali as the work of al-Qaeda--without bothering to gather a shred of evidence.</P>
<P>"These are nothing but cold-blooded killers," Bush told reporters. "They do not value life the way we value life in a civilized world." Value life? From the man pushing a barbaric new war against a country already devastated by bombs and sanctions?</P>
<P>Maybe Bush missed Secretary of Defense Donald Rumsfeld's latest orders to the Pentagon brass to draw up bigger and better attack plans for Iraq. The key, Rumsfeld says, is "lethality." "Looking at what was overwhelming force a decade or two decades ago, today, you can have overwhelming force, conceivably, with lesser numbers [of U.S. troops] because the lethality is equal to or greater than before," he gloated.</P>
<P>And now, it isn't only Republicans who are responsible for Washington's drive to war. Last week, half of the Democrats in Congress voted in favor of a resolution that gives Bush a free hand to attack Iraq.</P>
<P>Alleged liberal Sen. Hillary Rodham Clinton (D-N.Y.) claimed that she voted in favor of war because giving Bush bipartisan support would make his success at getting a resolution at the UN "more likely, and, therefore, war less likely." Who is she kidding? Congress handed Bush a blank check for war--and he's getting ready to cash it.</P>
<P>But outside the Washington establishment, more and more people are becoming convinced that Bush's war has nothing to do with "terrorism." It has everything to do with oil and expanding U.S. dominance in the Middle East.</P>
<P>That's why tens of thousands of people are organizing to travel to Washington, D.C., to protest on October 26. We have to send a message to Washington: Stop this war now!</P>

<I><P>Protest October 26! Washington, D.C., and San Francisco.</P></I>

