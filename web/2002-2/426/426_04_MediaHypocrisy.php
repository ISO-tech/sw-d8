<P><font face="Times New Roman, Times, serif" size="5"><b>Media hypocrites let loose on dockworkers</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 18, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>The corporate media have always been hypocritical. They criticize or laud aspects of society according to the ruling ideas, but at the same time, they have to appear close to the majority of their customers: the working class. </P>
<P>Recently, nothing has disgusted me more than the coverage of the dockworkers' lockout. As always, the media immediately bashed the union and workers, labeling them as corrupt, old style, refusing to modernize and greedy. In the <I>Wall Street Journal</I>, one article depicted the union leadership as 1950s-style mafia controlling half of the U.S. economy.</P>
<P>What U.S. bosses want are individually contracted workers who are flexible at will in order to make more profit. So those are the ideas that are being echoed in the corporate media.</P>
<P>What about keeping in touch with workers' concerns? Well, there have been countless articles in the press and reporting on TV showing the awful ordeal that the ex-employees of Enron, Global Crossing, Tyco, WorldCom, etc., have had to go through. Anchors and journalists pity these employees who have lost not just a job but years of savings, houses and insurance coverage. </P>
<P>If the media were genuine about their concern for laid-off workers, then they should encourage dockworkers fighting to keep their jobs and improve their living standards. Instead, we see condemnation of the dockworkers by journalists and TV hosts. </P>
<P>That's why workers need their own paper, a paper that is always on our side and never hypocritical. <I>Socialist Worker</I> is this kind of paper. </P>
<B><P>Frank Laporte,</B> New York City</P>

