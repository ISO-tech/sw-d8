<P><font face="Times New Roman, Times, serif" size="4">Taking on Boston's largest corporate cleaning service</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Janitors remain confident</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By David Brophy</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 18, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>BOSTON--Striking janitors in their third week on the picket line are fighting to improve their working conditions. Unicco Service Co., New England's largest janitorial contractor, is still refusing to budge on the union's demands for health care and job security. </P>
<P>Last week, six smaller contracting firms signed agreements to reduce the amount of time janitors must work to qualify for health insurance. Since then, important steps have been taken toward a victory in this inspiring strike of low-paid immigrant workers.</P>
<P>The campaign received a high-profile boost last week when Acting Massachusetts Gov. Jane Swift announced that she had cancelled Unicco's contract to clean four government buildings. Swift said she would be accepting bids on the government contracts from companies that would meet the workers' demands.</P>
<P>The loss of a $1.9 million contract might not make much of a dent in their $600 million revenue, but a public gesture of support from the typically anti-union State House is a bad sign for Unicco's millionaire executives.</P>
<P>Now corporate tenants are pressuring their building owners to follow suit. "If Jane Swift did it yesterday, we can do it today," said the tenants of 18 Tremont to the <I>Boston Globe</I> in explaining why they were pressuring the owners of the building to drop Unicco.</P>
<P>"The union is trying to make it look as if the streets are full of striking janitors by setting up picket lines with janitors bussed in from other cities, union interns, students and other agitators," fumed Donald Brecher of Janitronics.</P>
<P>But Swift's decision to cancel Unicco's contract came the morning after janitors working in state buildings walked off the job--and then began preparing to picket their own workplace! So much for the threadbare charge of "outside agitators."</P>
<P>The truth is that about 2,000 janitors continue to defy Brecher and the rest of Boston's corporate cleaning elite by staying away from work--and their confidence is high. "The strike is going really well," one janitor told <I>Socialist Worker</I>, "and I think we're going to win, because we have the support of so many people."</P>
<P>Not a moment too soon, Service Employees International Union (SEIU) stepped in to provide members with strike pay at half their regular wages for the next two months. And activists have rallied to their cause--14 supporters were arrested for civil disobedience last Thursday after a student walkout drew hundreds. </P>
<P>Workers around the country have been taking notice, too. Janitors in Hartford struck for a day to show their solidarity, and actions have also been staged in Pittsburgh, Los Angeles, San Francisco, Sacramento, Calif., and Seattle.</P>
<P>Not surprisingly, the strike's opponents have focussed their energy on getting the workers out of the public eye--but without success. Last week, the courts extended an injunction to allow the janitors to march on the streets with 12 hours' notice--a right accorded striking workers under federal law.</P>
<P>Police commissioner Paul Evans called the court's decision to let the workers march "a recipe for disaster," while transport commissioner Andrea D'Amato argued that rallies cause "real public safety concerns." </P>
<P>Of course, what they're really concerned about is the effect that a spirited fightback by one section of Boston's underpaid could have on the rest of the city's working poor. If the strike bug catches on, it really would be a disaster for defenders of the status quo like Evans and D'Amato.</P>
<P>But it would be a real boost for those who support justice--not just for janitors but for workers everywhere.</P>

