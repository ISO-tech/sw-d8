<P><font face="Times New Roman, Times, serif" size="5"><b>Obituary: Tony Mazzocchi</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Bill Roberts</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 18, 2002 | Page 4</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>TONY MAZZOCCHI, a longtime union official and founding member of the Labor Party, died at the age of 76.</P>
<P>Mazzocchi's feisty presence in the workers' movement will be missed. He had a long association with the Oil, Chemical and Atomic Workers (OCAW) union in various capacities--and served as secretary-treasurer from 1988 to 1991.</P>
<P>As an official in OCAW, he confronted what he sometimes referred to as the "criminal class." "The chemical industrialists are mad bombers," he said. "That's the big crime-in-the-street story. It is not your local mugger, it's an industry that mugs people by the thousands."</P>
<P>He's credited with being a prime mover behind the Occupational Safety and Health Act, signed into law by President Richard Nixon in 1970, and he was instrumental in helping Karen Silkwood prepare her case against Kerr-McGee's falsification of records at its nuclear fuel plant in Oklahoma--a story that was dramatized in the movie <I>Silkwood.</P>
</I><P>After his retirement from OCAW in 1991, Mazzocchi devoted his time to building a labor party and presided over the founding convention of the Labor Party in 1996. His primary motive was to give organized workers a bigger voice in the political process.</P>
<P>Mazzocchi was disappointed at how far to the right the Democrats had moved since the New Deal of the 1930s. He hoped that over time a party of labor could speak for the forces of progressives. In the meantime, the Labor Party could act as a pressure group on the Democrats, as well as the labor bureaucracy.</P>
<P>Mazzocchi was a militant reformer who believed in the power of organized labor. As a labor official in the 1980s, though, his vision sometimes narrowed to exclude an active rank and file in favor of the "wisdom" of a narrow band of brothers at the top of labor. This narrower vision contributed to his limited perspective for the Labor Party as a group to pressure the Democrats.</P>
<P>I prefer the perspective that Mazzocchi endorsed in 1970: "When you build a big movement from down below, regardless of who's in the White House, you can bring about change."</P>

