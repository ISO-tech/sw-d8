<P><font face="Times New Roman, Times, serif" size="5"><b>Building labor's opposition to the war</P></b></font>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 18, 2002 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>MORE UNIONS are joining the opposition to the U.S. war on Iraq. Last week, Service Employees International Union Local 1199 in New York took out a full-page ad in the <I>New York Times</I> opposing Bush's drive to war. And this weekend, union members will gather for a conference organized by New York City Labor Against War (NYCLAW).</P>
<P>A slew of labor organizations have endorsed the conference, and both the Albany and Rochester Labor Councils passed antiwar resolutions that endorsed mobilizing for the October 26 march against the war in Washington, D.C.</P>
<P>"The NYCLAW conference is important, particularly during the current drive to war in Iraq," Bernie from the United Federation of Teachers told <I>Socialist Worker</I>. "Labor can play a key role in organizing against this war. The NYCLAW conference will provide a crucial opportunity for people to share ideas about how to organize against this war in our unions and workplaces. Instead of spending billions of dollars on a war for oil, that money should be spent on education, jobs and health care." </P>
<P>For many coming to the conference, opposing the war flows out of opposing Bush's whole agenda. "Hell no, I don't trust George Bush getting us into a war," explained Dara Harper from the New York State Nurses Association. "Organizing will prevent people from getting killed, our sons and daughters. I think the biggest problem we have in America today isn't Iraq, it's the politicians and Bush leading us into disaster."</P>
<P>Wilma Claude from AFSCME Local 371 is going to the NYCLAW conference with the conviction that organizing efforts today will bring change tomorrow. "I know some people wonder if it will really make a difference because it appears the government is bent on going to war, and they always find a way to accomplish their goal," Claude told <I>Socialist Worker</I>. "Well, the anti-Vietnam War marches and demonstrations helped to stop it. From past experience, history has shown that organizing does work."</P>
<P>The Vietnam War also illustrates the general rule that war abroad goes hand in hand with attacks on workers at home. "I really don't feel we should go to war," said Jose Rodriguez of AFSCME Local 420. "It's about a country that has oil that we want. It's totally unacceptable. Organizing to have thousands of people saying war isn't the answer can make the government pay attention. There's a lot of things here that need the money--housing, medical care. Let's tell Bush to go out there--you go fight Saddam if you want to get rid of him."</P>
<P>The truth is that it's not Iraq but the U.S. that must be stopped. "The U.S. war on Iraq proves again that the world's most dangerous rogue state is the United States," explains Michael Letwin, president of the Association of Legal Aid Attorneys and NYCLAW's co-convenor. "Just as workers on 9-11 suffered the painful blowback from previous U.S. policy, workers will lose many times over for an Iraq war. In social services, wages, jobs, civil rights, immigrant rights, labor rights and blood."</P>
<P>"But workers also have a unparalleled potential power to end the war. NYCLAW's conference aims to help organize that power."</P>
