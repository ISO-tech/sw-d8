<P><font face="Times New Roman, Times, serif" size="5"><b>Terror and injustice in Jim Crow South</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Keeanga-Yamahtta Taylor</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 18, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>DOCUMENTARY: <I>The Rise and Fall of Jim Crow,</B></I> a four-part PBS series airing in October. Check local listings for show times.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>THROUGHOUT OCTOBER, PBS is showing a powerful documentary series on Jim Crow--the system of legalized segregation that was the law of the land in the American South until 1965. It compiles photographs and interviews with historians and veterans of the civil rights movement in an attempt to explain how legalized racism could exist in a country in which freedom and democracy were supposedly enshrined in its Constitution.</P>
<P>This film comes at an important time, with a debate raging over whether African Americans are entitled to reparations because of slavery and the legalized injustice that came in its aftermath. <I>The Rise and Fall of Jim Crow</I> makes an airtight case about the extreme injustice meted out on Blacks well after slavery's abolition.</P>
<P>While Jim Crow laws were aimed at physically dividing Blacks and whites, the racist atmosphere that was conjured up to explain why whites should be separated from Blacks fueled more racist violence. Some of the most brutal examples were numerous "race riots"--anti-Black rampages by racist whites--that resulted in the deaths of hundreds of Blacks in the early part of the century.</P>
<P>One of the most outrageous examples was in Tulsa, Okla., where 6,000 Blacks were rounded up by local authorities, 300 Blacks were murdered and 35 city blocks in a Black neighborhood were burned to the ground in 1921.</P>
<P>The documentary also looks at how Southern officials got around the abolition of slavery by using the "convict-leasing" system to convict Blacks of the most ridiculous crimes and then "lease" them out to business owners as part of their sentence. A 6-year old Black girl was sentenced to 30 days in jail for "stealing" a hat. A 12-year-old Black boy was sentenced to 20 years for stealing a horse that was too big for him to ride.</P>
<P>All in all, terrorism was used to underpin segregation and uphold white supremacy in the South while officials in the North gave a wink and a nod.</P>
<P>But one problem with the documentary is that it doesn't adequately explain why Jim Crow came into existence and why it continued well into the 20th century.</P>
<P>The film argues that the first generation of Blacks that came up after slavery were "too uppity" in the eyes of white Southerners. In other words, Jim Crow was a tool of all whites to put all Blacks in their place.</P>
<P>This doesn't stand up to historical evidence of the collaborative efforts of Black and white farmers and workers in the South in the period after the Civil War--the Populist movement--to challenge the power of the Southern ruling class. Moreover, it doesn't speak to the fact that the white Southern elite used the violent racism against Blacks to undermine the rights and aspirations of poor whites as well.</P>
<P>The documentary rightly points out that white supremacist politicians in the South used the racist poll tax to disenfranchise Black voters, but forgets that in the process several hundreds of thousands of poor whites were disenfranchised as well. This is not to minimize the oppression that Blacks faced, but to point out who exactly benefited from this oppression and who had an interest in perpetuating it.</P>
<P>Nonetheless, <I>The Rise and Fall of Jim Crow</I> is well worth watching to get a startling view of America's racist history--and the struggles of ordinary people against it.</P>

