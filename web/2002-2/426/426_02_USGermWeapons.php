<P><font face="Times New Roman, Times, serif" size="5"><b>When the U.S. used germ weapons on "its own people"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 18, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH never tires of denouncing Saddam Hussein for using chemical and biological weapons "against his own people."</P>
<P>But it's a good bet that Bush won't have anything to say about the U.S. government--even though, according to recently released Defense Department documents, the U.S. military carried out dozens of open-air chemical and biological weapons tests on U.S. soldiers in Alaska, Hawaii, Maryland and Florida between 1962 and 1973. Among the chemical weapons used in the tests were VX, sarin, soman, tabun and a biological agent that's in the same family as anthrax.</P>
<P>The Pentagon says it's trying to identify as many as 5,500 soldiers and military personnel who may have been exposed in the tests. Pentagon officials say that they're "concerned" about these men. But there are still dozens more reports on tests that the military is refusing to make public. The Pentagon won't provide proof that civilians were never exposed to the poisons--or even apologize to soldiers who were exposed.</P>
<P>So exposing "your people" to deadly chemical weapons is fine--as long as it's the U.S. government doing it.</P>

