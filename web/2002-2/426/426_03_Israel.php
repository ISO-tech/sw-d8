<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Israel's scorched-earth war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 18, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>LAST WEEK, an Israeli helicopter gunship fired a missile into a crowded residential neighborhood in the Gaza town of Khan Younis, killing 16 Palestinians and wounding 130 more. Though criticism poured in from around the world, Israeli Prime Minister Ariel Sharon called the attack a "success"--and vowed that more such operations would follow.</P>
<P>Inside Israel, there was open speculation that the assault was a practice run for the kind of invasion that Israeli forces would mount under the cover of a U.S. war on Iraq. Every day, the Israeli press is filled with discussion of such horrors. The <I>Jerusalem Post,</I> for example, ran an op-ed article calling on the U.S. to topple the regime in Iraq--and use the land for a Palestinian state.</P>
<P>"If Washington is already in the mood for thinking big," wrote columnist Yosef Goell, "it should&#133;restructure that part of the Middle East, and solve several of the region's intractable problems along the way. After creating an independent or autonomous Kurdistan in its northern third, the southern two-thirds of Iraq should be merged with Jordan and reconstituted&#133;to incorporate the Palestinian territories and population."</P>
<P>Long considered an extremist fantasy, talk of mass expulsion is now an increasingly mainstream view. But even as the list of atrocities grows--and the prospect of all-out ethnic cleansing gains favor--those in the U.S. who dare to criticize Israel's war on Palestinians find themselves under attack.</P>
<P>Last weekend, about 400 pro-Palestinian students, academics and activists gathered at the University of Michigan to discuss how to organize a campaign to pressure U.S. institutions to divest from Israel--just as students built a movement in the 1980s to push for divestment from South Africa. Pro-Israel students mobilized from throughout the region to stand outside the conference, holding picket signs that read: "This is an anti-Semitic hate conference."</P>
<P>This is utter dishonesty. "We categorically reject the accusations of anti-Semitism that are being tossed around as part of a desperate effort to discredit the movement," said Ora Wise, a Jewish student from Ohio State University who helped to organize the Michigan conference. "As Jews fighting for Palestinian liberation, we are struggling for Jewish emancipation from the reactionary, narrow, uncritical mentalities of those Jews who make it impossible."</P>
<P>Sharon's assault on Khan Younis shows that the main perpetrator of violence and terrorism is Israel. For decades, Israel's theft of Palestinian land has been upheld by a nasty combination of racist justifications and naked repression.</P>
<P>Washington's threats of war against Iraq for its supposed violations of United Nations resolutions--when the whole world knows that Israel has violated countless such resolutions--only serve to expose the U.S. government's own complicity in Israel's vicious war.</P>
<P>As long as Israel's colonial occupation continues, so will the resistance to it. That's why we say: Free Palestine!</P>

