<P><font face="Times New Roman, Times, serif" size="5"><b>UC hit by two-day strike</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Martin Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 18, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SAN FRANCISCO--More than 1,000 lecturers at several University of California (UC) campuses walked off the job as <I>Socialist Worker</I> went to press. Instead of teaching, the lecturers picketed as part of a two-day strike to pressure the administration to compromise on contract issues.</P>
<P>Organizers say that this will be the largest strike UC has ever witnessed. In total, eight UC campuses will witness strikes on October 14 and 15 by the American Federation of Teachers (AFT), the non-tenured lecturers' union.</P>
<P>And four of these campuses will face joint strikes by both AFT and the Coalition of University Employees (CUE), a predominantly female union, which represents more than 18,000 clerical workers. </P>
<P>Lecturers' system-wide have been without a contract for two years and clerical workers for one year. In fact, CUE, AFT and other campus unions are charging UC with unfair labor practices, bad faith bargaining and outright lies. "It seems UC's poor treatment and lack of respect toward all the bargaining units has finally gone too far," said one AFT member.</P>
<P>For the AFT, the primary issue is lack of job security and mistreatment of non-tenured lecturers, a common grievance at universities across the U.S. Lecturers are hired as temporary workers on a year-to-year basis, and they are the only faculty judged solely on the basis of their teaching. </P>
<P>Lecturers teach almost half of UC's undergraduate courses, and their teaching loads are two to three times heavier than the loads of their tenure-track colleagues. For CUE, the strike is in part about UC's unfair labor practices--including retaliation against union bargainers and unilateral changes in work rules, layoff procedures and performance evaluations. </P>
<P>In addition, CUE members are angry at UC's regressive bargaining strategy. The administration first offered a 2.5 percent increase, but then said it would offer no raise because of California's budget crisis. But top administrators with six-figure salaries got raises of 8 to 26 percent!</P>
<P>The solidarity between the various campus unions on strike is historic. Other unions who aren't on strike--both on and off campus--have also pledged their support. University Professional and Technical Employees have vowed not to cross the lecturers' picket lines. And the Service Employees International Union Local 415, which just won a successful strike by county workers in Santa Cruz, has pledged to walk the picket lines and is allowing their union hall to be used for strike organizing.</P>
<P>"This strike is about flexing our muscle," one CUE member told <I>Socialist Worker</I>. "We will show UC that they must take us seriously. This strike is only the beginning."</P>

