<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Is socialism against our human nature?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 18, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>OF ALL the arguments against socialism, the most persistent is that a socialist society could never work because human nature is flawed--that people are by nature greedy, selfish and violent.</P>
<P>It's not surprising that people might feel this way. From the time that we're children, capitalism forces working people to compete against one another every day--in school, to get a job, at work. The media constantly reinforce the idea that people are only out for themselves--and that they're willing to do terrible things to each other.</P>
<P>But this isn't the full picture. The truth is that some people are too greedy. Like Microsoft tycoon Bill Gates, whose personal wealth is bigger than that of many poor countries. Or former Tyco CEO Dennis Kozlowski, who used company money to buy a $15,000 umbrella stand. Most ordinary people never know what it's like to be greedy on this scale. They rarely, if ever, get to splurge on themselves.</P>
<P>Yet at the same time, working people make incredible sacrifices for their families and friends--or sometimes even complete strangers facing hard times. Just think of the hundreds of thousands of ordinary people who donated money and time to help families of those who died in the World Trade Center attacks.</P>
<P>The idea of what gets classified as greedy and what doesn't is set by the people at the top of society. So George W. Bush has no problem spending $400 billion on the military--about as much as every other government in the world combined. But he implies that West Coast dockworkers fighting for job security are greedy.</P>
<P>This is commonplace--when working people struggle for a share of the profits that they created, they're written off as "greedy." But, to take the case of the dockworkers, while they make on average more than $80,000 a year, that's nothing compared to the $300 billion worth of goods that they move each year--or the profits that the shipping bosses make off their labor.</P>
<P>The media mouthpieces who complain about dockworkers' "greed" never talk about how these workers achieved relatively higher wages in the first place--they organized a union and fought to get them.</P>
<P>Some people believe that more "sacrifice" is what's needed. They think that to overcome the selfishness of the everyday world, working people should consume less--give up their cars maybe, and not focus on material possessions. But "sacrifice" is what working people are forced to do every day.</P>
<P>The circumstances of scarcity--of not having enough--is what gives rise to the dog-eat-dog logic of the world we live in. If the basic needs of all people for food, shelter and so on were guaranteed, then the motive for most crime, for example--poverty and desperation--would be ended.</P>
<P>If society wasn't organized around the bosses' drive for profit--and as a result, the drive to make workers work harder for less--then the reason for competition among working people would disappear.</P>
<P>Under capitalism, the majority of people are forced to fight over the crumbs left behind by the tiny minority at the top. We want a different kind of society--one in which workers aren't forced to compete, but instead can put our combined resources and cooperative efforts into lifting everyone to a more fulfilling life.</P>
<P>This isn't a pipe dream, either. You can see the potential of a society based on equality and solidarity when people come together in struggle--to fight for some measure that will make the world a better place.</P>
<P>People change when circumstances change. But the process of changing those circumstances changes people, too. As Karl Marx put it: "Revolution is necessary not only because the ruling class cannot be overthrown in any other way but also because the class overthrowing it can only in a revolution succeed in ridding itself of all the muck of ages and become fit to found society anew."</P>

