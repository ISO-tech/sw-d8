<P><font face="Times New Roman, Times, serif" size="4">University helped Dubya bail out of Harken disaster</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Bush's dirty deal with Harvard</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 18, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"IT'S LEGAL, but God, it smells rotten." That was the conclusion reached by Bill Coyle, an accounting expert at Babson College, after looking at the web of insider deals and accounting scams linking George W. Bush and the Harvard Management Co.</P>
<P>The Bush-Harvard connection is just the latest in a series of scandals that has threatened all year to turn the Bush administration into a line-up of Corporate America's Most Wanted.</P>
<P>Harvard Management was set up to manage Harvard University's multibillion-dollar endowment. In 1986, it sank $20 million in an obscure oil company called Harken Energy--shortly after Harken brought Dubya, then the bumbling son of the vice president of the United States, onto its board of directors after buying Bush Jr.'s own bankrupt oil venture.</P>
<P>Harvard Management soon came to own nearly a third of Harken, but began selling off its shares in the early 1990s. Though not before Bush and the Harken board established an Enron-style partnership with Harvard Management to move $20 million in debt off Harken's books. By hiding this debt, the deal helped to prop up Harken's stock price.</P>
<P>Harvard started selling its Harken shares after the stock price nearly tripled to $8 a share--today, the stock trades at about 40 cents. Then, in 1990, Bush sold 212,140 Harken shares--just before the company released bad financial news.</P>
<P>But openly selling this much stock--nearly 20 times the daily average of Harken stock trades--would have sent the price plummeting. So how did Bush get out without setting off alarm bells? According to Charles Lewis of the Center for Public Integrity, all the available evidence suggests that Harvard Management bought Bush's shares after a Bush stockbroker arranged the deal.</P>
<P>Finally, in 1995, it was payback time for Harvard. Shortly after Bush's election as governor of Texas, the state Teacher Retirement System sold a piece of real estate--the Anatole Hotel--on favorable terms to Harvard Management. Without taking bids, the fund sold the hotel for $27 million less than what it had paid simply to refurbish the building!</P>
<P>Today, Bush likes to praise "hard-working Americans" while denouncing "corporate criminals." And his Treasury Secretary Paul O'Neill says that corporate executives who defraud investors should "hang from the highest branch." Don't expect to see his boss in the trees any time soon. </P>

