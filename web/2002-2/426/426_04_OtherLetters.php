<p><a name="lies"></a><font face="Times New Roman, Times, serif" size="4"><b>Don't believe Cheney's lies</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>, </P>
</B><P>A recent article in the <I>New York Times</I> reported that Vice President Dick Cheney claims to have evidence that Iraq is supporting--and training--members of al-Qaeda.</P>
<P>If Cheney really has the evidence that he claims, then why would there be any concern about not getting support for a new war in the United Nations (UN)? Also, why wouldn't that evidence have been presented to the UN? </P>
<P>The truth of the matter is that the evidence doesn't exist. If it did exist, then there would be very little debate over the invasion plans in the UN or in Congress. There would be virtually across-the-table support for a new war.</P>
<P>This seems to be a last attempt at connecting Iraq with the atrocities on September 11. Every other attempt has proven to be in vain, and there is little doubt in my mind that this is just yet another lie in order to justify the coming war.</P>
<B><P>Russell Pryor,</B> Columbus, Ga.</P>

