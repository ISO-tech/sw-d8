<P><font face="Times New Roman, Times, serif" size="4"><b>The Bush gang's sick war on drugs</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>On September 17, more than 1,000 supporters for the medicinal use of cannabis gathered at Santa Cruz city hall as the Wo/Men's Alliance for Medical Marijuana (WAMM) handed out prescription marijuana to patients in defiance of federal agents. Patients with AIDS, cancer and other terminal illnesses came forward. As one told the crowd, "I am not the enemy."</P>
<P>The protest was sparked by a September 5 raid by the Drug Enforcement Administration (DEA) at the WAMM collective. DEA agents swooped down on the co-op, pointing automatic weapons at paralyzed patients, tore up WAMM's garden and carted off the operators in handcuffs. Such terror and heavy-handed tactics were unwarranted in light of the serious illnesses facing the patients. </P>
<P>But even more, Californians overwhelmingly voted in favor of the legalization of medical marijuana in 1996. The attack on WAMM by the DEA shows that Bush and his cronies have no regard for the democratic process. </P>
<P>The hypocrisy is apparent, as Bush claims to support states' rights only when it comes to anti-abortion laws or school vouchers, but conveniently disregards the will of the people when they oppose his and Attorney General John Ashcroft's right-wing agenda. </P>
<P>As Bush escalates his war plans abroad, he is also increasing the war against the sick and ill at home. </P>
<B><P>Martin Smith,</B> Santa Cruz, Calif.</P>

