<P><font face="Times New Roman, Times, serif" size="5"><b>Bush's corporate crime cop a crook</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 23, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>EARLIER THIS year, when George W. Bush was "getting tough" on crooked CEOs, he named Deputy Attorney General Larry Thompson to head up his corporate crime task force. Turns out Thompson is exceptionally "well-qualified" for the position.</P>
<P>According to a lawsuit filed last week by the corporate watchdog group Judicial Watch, Thompson will know just what to look for--since he himself helped cook the books as an executive at Providian, the credit card company known for charging astronomical interest rates to poor people.</P>
<P>Thompson was chair of Providian's Audit and Compliance Committee for several years before he joined the Bush administration's Justice Department. The suit charges that Thompson and other officers hid the company's worsening financial condition in 2001--artificially inflating Providian's stock price while Thompson and others exercised their stock options. Thompson himself sold $4.7 million worth of stock--a few months before the share price tanked.</P>
<P>After heading for Washington, Thompson used his official position as deputy attorney general to block any Justice Department action against the acts of fraud that he and his friends at Providian committed.</P>
<P>"Whether it is the Enron, Arthur Andersen, Global Crossing, Halliburton, Harken, or now the Providian scandal, there is a dangerous intersection between politicians of all stripes, Democrat and Republican, attempting to feed at the trough of business greed," said Judicial Watch head Larry Klayman.</P>

