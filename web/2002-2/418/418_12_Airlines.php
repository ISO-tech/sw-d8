<P><font face="Times New Roman, Times, serif" size="4">Making workers pay the price</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Airlines bosses lower the ax</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 23, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CORPORATE AMERICA is cutting more jobs--and airline bosses are leading the way. This month's bankruptcy filing at US Airways--followed by United Airlines' threat to make the same move--signals an all-out assault on union jobs and benefits across the industry.</P>
<P>Airlines are using the falloff in air travel since September 11 as an excuse for making the cuts. Since then, they have eliminated 90,000 jobs and lost about $9 billion. But the truth is that airlines were in crisis before September 11. </P>
<P>The bosses are using the attacks to justify making workers pay for the recession. Leading the attack is US Airways, which wants $950 million in concessions from unions. The Airline Pilots Association plans to vote this month on a deal approved by union leaders for a six-and-a-half-year contract that contains a pay cut of 26 percent.</P>
<P>But US Airways filed for bankruptcy anyway--because "the long road to reorganization can offer airlines a useful opportunity to streamline their operations and gain new financing," <I>the New York Times </I>reported.</P>
<P>Translation: Bankruptcy gives employers more leverage. The US Airways bankruptcy gave American Airlines the excuse to eliminate 7,200 jobs--mostly from TWA, which the company took over with a promise to unions not to lay off anyone from that company.</P>
<P>Then United Airlines threatened to file for bankruptcy unless unions gave up more. The International Association of Machinists (IAM), which represents mechanics and ramp workers, had already agreed to defer nearly $500 million in back pay. And the pilots' union had agreed to a 10 percent wage cut in exchange for $520 million more stock from the company's Employee Stock Ownership Program (ESOP)--as long as other unions go along. Now United is threatening bankruptcy anyway--though it still has $2 billion in cash.</P>
<P>Spurring the airlines' attacks on unions is the government's Air Transportation Stabilization Board, created as part of the $15 billion airline bailout after September 11. The board insists on "cuts in labor costs" as a precondition for any federal loan guarantee--that is, wage cuts, work rule concessions and reduction in benefits.</P>
<P>But the airlines aren't the only high-profile businesses axing jobs. In recent weeks, mass layoffs have been announced by IBM and Hewlett-Packard. And Boeing declared that 33,000 workers laid off in recent months will likely never return. </P>
<P>Long-term unemployment--those jobless for 26 weeks or more--is worse now than in any of the last four recessions, according to a study by the Economic Policy Institute. "As of June 1, over 1.4 million workers were collecting up to 13 weeks of federal extended benefits," the study found. "Nearly 900,000 of those workers have now run out of benefits even though the labor market shows no signs of lasting recovery."</P>
<P>The corporate scandals from Enron to WorldCom have revealed how crooked CEOs and profit-hungry employers ripped off working people during the boom years of the 1990s. Now they want to make workers pay for their crisis.</P>
<P>It's time to draw the line against corporate greed--and organize to defend our unions and our jobs.</P>

