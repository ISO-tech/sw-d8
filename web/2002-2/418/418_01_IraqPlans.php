<P><font face="Times New Roman, Times, serif" size="4">Stop Bush's war drive!</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Planning another slaughter in Iraq</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 23, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>NO COUNTRY since Vietnam has suffered more destruction from U.S. military power than Iraq. The 1991 Gulf War killed an estimated 200,000 people in a period of just six weeks. U.S. warplanes dropped more tons of bombs on Iraq faster than in any other aerial bombardment in the history of warfare.</P>
<P>And when the "war" was over and Iraqi soldiers and civilians were retreating, the U.S. attacked again--turning the road from Kuwait to Basra into the "highway of death."</P>
<P>Then came a decade of economic warfare--in the form of United Nations sanctions. Diseases that had been wiped out decades ago now flourish in Iraq because of shortages of basic medical supplies. Some 5,000 children die every single month because of sanctions.</P>
<P>But still George W. Bush isn't satisfied. He wants more blood--and the only question is when, not if. The administration's saber rattling has grown louder over the past several weeks, with more and more "leaked" war plans showing up in the media.</P>
<P>The latest scenario is called the "inside-out" plan. U.S. forces would go after the Iraqi capital of Baghdad--with warplanes pounding a city of 5 million people and troops carrying out operations in the streets.</P>
<P>The horrors of such an assault are impossible to imagine. But Bush was convening a meeting of his war team--Dick Cheney, Donald Rumsfeld, Condoleezza Rice, among others--earlier this week to step up preparations.</P>
<P>The White House claims that it's after the "evil" Saddam Hussein. Of course, many of the same officials running Bush's war machine today were extending the hand of friendship to Saddam in the 1980s--when the Reagan administration decided to back Iraq in the Iran-Iraq War. This with the full knowledge that Saddam possessed chemical weapons and was prepared to use them, according to a <I>New York Times</I> report on August 18. </P>
<P>Bush's plans for war have nothing to do with concerns about democracy or justice. "What talks in the region? Power," raved right-wing <I>Washington Post</I> columnist Charles Krauthammer earlier this year. "Fear. Respect for American power."</P>
<P>The Bush gang is ready to wreak further havoc in Iraq to show that the U.S. government can do whatever it likes around the globe.</P>
<P>Over the last week, a debate has broken out within the Washington establishment about Bush's war plans. Some veteran hawks--like Brent Scowcroft, the former national security adviser and tight Bush family friend, and former Secretary of State Henry Kissinger--publicly opposed launching a new attack. In the <I>Wall Street Journal,</I> Scowcroft warned that an invasion could unleash "an Armageddon in the Middle East."</P>
<P>These hawks haven't turned into doves. They're just worried that this isn't the right time or the right reason to go after Iraq. After all, the Bush administration has failed to produce any link at all between Saddam and the September 11 attacks--the main justification for Bush's "war on terrorism."</P>
<P>The U.S. doesn't have any evidence that the Iraqi regime is producing the "weapons of mass destruction" that Bush claims. And even staunch U.S. allies in Europe and the Middle East are opposing any new U.S. action against Iraq.</P>
<P>Given all the lies that Washington has told during its race to war, it's no surprise that a majority of people say they support military action against Iraq to oust Saddam. What's surprising, in fact, is the growing numbers who have doubts--who have begun to question the administration's claim that a devastated and poverty-stricken society represents a threat to the U.S. These doubts will only grow as the U.S. moves closer to an all-out assault.</P>
<P>Iraq has been devastated by more than a decade of U.S. war. But the Bush gang has even worse in store. We have to stand up and speak out against Bush's new war on Iraq before it begins.</P>

