<P><font face="Times New Roman, Times, serif" size="5"><b>UPS should give us more</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Donny Schraffenberger, steward, Teamsters Local 705</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 23, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--The recent contract negotiations between the Teamsters and United Parcel Service (UPS) have exposed the weaknesses of the national Teamsters leadership and its lawyer president, James Hoffa.</P>
<P>The pressure to come to a quick agreement with tough companies like UPS, without waging a real fight, means that we end up settling for far less than what we could have received with a real show of force.</P>
<P>This weakness has also permeated Teamster locals--including Chicago Local 705, led by Secretary-Treasurer Jerry Zero, a longtime reformer who now supports Hoffa. At a recent Local 705 stewards meeting, Zero praised the local negotiating committee for the tentative contract. This is "the best contract we ever got at UPS," said Zero. "No, it isn't perfect, but no contract is."</P>
<P>This six-year deal is highly unpopular with many stewards and rank-and-file members at the Jefferson St. hub where I work. But when those critical of the proposed UPS agreement spoke out at the stewards meeting, Zero became irate. Union democracy should mean that we reasonably debate this agreement--the good aspects as well as the bad.</P>
<P>Our union is capable of fighting back. A number of stewards and business agents were recently arrested for trying to get information to workers during a union decertification attempt at Kmart. We need that same kind of fight at UPS.</P>
<P>We, the members, have to live with this agreement, and six years is a long time to be dissatisfied. Remember that UPS has had five years of record-making profits. The company can afford to give us more in this contract.</P>
<P>Here's a list of reasonable changes to the Local 705 agreement:</P>
<P>--Create full-time jobs in the first not the second year of the contract</P>
<P>--One pay scale for air combo workers based on full-time rates not part-time</P>
<P>--Make all nonunion temp jobs into full-time union work</P>
<P>--Capture the cross-town UPS rail routes for Local 705 feeder drivers from nonunion truckers</P>
<P>Nationally, we need to raise part-time base pay to $12 an hour. We need a three-year, not a six-year, contract. We need to create full-time jobs in the first year of the contract instead of the third year.</P>
<P>All Local 705 UPS members should attend the UPS Tentative Agreement Review meeting at the Local 705 auditorium (corner of Jackson and Ashland), September 7, 9 a.m. sharp.</P>
<P>We need to have our voices heard. A "no" vote on the contract means the negotiating committee has to go back to the table.</P>

