<P><font face="Times New Roman, Times, serif" size="4">ILWU dockworkers send a message to Washington:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Hands off our union</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 23, 2002 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>RALLIES BY dockworkers up and down the West Coast last week sent a message to George W. Bush: Keep your hands off our union! Thousands of dockworkers and their supporters turned out at five major ports for demonstrations called by the International Longshore and Warehouse Union (ILWU).</P>
<P>The ILWU wanted to put pressure on employers as negotiations resumed last week. But the protests were also a reaction to threats by the Bush administration since the union's contract expired July 1.</P>
<P>According to press reports, the White House has threatened the use of the Taft-Hartley Act to impose an 80-day "cooling-off period" and threatened to use troops to move cargo in the event of a strike or work slowdown. The government is also threatening anti-trust legal action to break up the ILWU's coast-wide contract. </P>
<P>No wonder the port bosses--who negotiate as a group through the Pacific Maritime Association (PMA)--have become even bolder in their determination to ram through a concessionary contract that would slash 1,500 jobs and eliminate the union-run hiring hall.</P>
<P>At the union rallies last week, one speaker after another vowed that the labor movement wouldn't tolerate this attack by Washington. "George W. Bush was born with a silver spoon in his mouth," Robbie Stern of the Washington State Labor Council told a crowd of 1,500 in Seattle. "He doesn't understand what it's like to work&#133;All he knows is that his wealth keeps going up, up, up. Well, we're going to bring him down!"</P>
<P>But unfortunately, the words of union leaders haven't been backed up by preparations for action--even within the ILWU itself. In calling on the government to stay out of negotiations, ILWU President James Spinosa emphasized the union's willingness to cooperate. "We're asking them to let the system work," Spinosa said at the Oakland rally. "Let the system work like it's worked for the last 30 years--without one strike in over 30 years." </P>
<P>Spinosa outlined the concessions that the union has already given, including the elimination of as many as 1,500 clerk jobs. "I can tell you we stepped up to the plate," Spinosa said. "Where is the PMA?"</P>
<P>But with every retreat by the union, the PMA has only demanded more. And so far, the ILWU has refused to take a strike vote or prepare job actions--like a work slowdown, which was effective at forcing a decent contract out of the PMA in 1999. Effectively, the union has imposed a "cooling-off period" on itself.</P>
<P>The union is under attack because $300 billion worth of goods that come through West Coast ports each year pass through their hands.  That's why the Bush administration's union-busting threats are very real. But the only way that the anti-union tide will be turned is when workers take a stand--and the whole labor movement turns out to defend them.</P>
<P>There was another problem at last week's rally as well. At both the Long Beach and Oakland rallies, one of the signs provided by the union read, "Fight terrorism, not workers." But this plays right into the Bush administration's hands--the government is justifying its threat to intervene precisely on the basis of a "national emergency." Waving the flag will make it that much harder to challenge Bush's union busting.</P>
<P>The ILWU is planning more rallies, and the AFL-CIO has called for unions across the country to show their support for the dockworkers at Labor Day events. But as one member of ILWU Local 10 told <I>Socialist Worker,</I> "We need some kind of action. We need to strike."</P>
<P>If dockworkers used their power, they could challenge the bosses' drive for concessions--and strike a blow for the entire labor movement.</P>
<I><P>Brian Belknap, Darrin Hoop, Steve Leigh, Amanda Maystead, Federico Moreno and Ken Morgan contributed to this report.</P></I>

