<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>New trouble ahead for U.S. economy?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 23, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>EARLIER THIS year, most economists were happily predicting that last year's recession was neatly concluded and an economic recovery was underway. But after a steady stream of statistics showing continuing problems for the economy, the analysts have changed their tune--with some even raising the prospect of a double-dip recession, where a recovering economy backslides into a slump again.</P>
<P>What happened? One factor is the huge summer decline in the stock market, which lost about 20 percent of its value in July alone. The sell-off began after a new wave of corporate accounting scandals, and Wall Street and corporations are spooked by the instability.</P>
<P>Another factor is the Commerce Department's admission at the end of July that last year's recession was deeper than previously thought.</P>
<P>But looming over all of these is a weakness in the economy that has only grown worse this year--"overcapacity." This term means that corporations have more machines, technological resources and factories--more capacity to produce--than they can use profitably. </P>
<P>U.S. industry was operating at only 76 percent of full capacity in June, pushing toward a 20-year low. When industry is suffering from overcapacity, corporations have no reason to make new investments, and the debt hangover from paying for capacity that isn't even being used drives companies to cut costs--that is, lay off workers and close plants.</P>
<P>In the face of growing jitters about the economy, the Bush White House has had little to offer. At his made-for-TV economic forum in Waco, Texas, the best Dubya could manage was: "There may be some tough times here in America, but this country has gone through tough times before."</P>
<P>Cold comfort for the 7,000 workers at American Airlines who learned that very day that they would lose their jobs.</P>
<P>The truth is that the federal government has few solutions. Last week, the Federal Reserve kept interest rates unchanged, but said it could cut them in the coming months--a tactic used to spur economic growth by making it cheaper for corporations to borrow money for new investments.</P>
<P>But interest rates are already at record lows. The problem of overcapacity means that corporations aren't likely to make new investments, even if they could borrow money at no interest.</P>
<P>Despite all this, some economists hope that strong consumer spending could still pull the economy into a sustained recovery. Car sales continued to jump over the summer--thanks to zero-interest financing made possible by the Feds' low interest rates.</P>
<P>But at the same time, personal debt levels continue to grow. And the booming housing market has produced a bubble in real-estate values. If the bubble bursts--maybe under the impact of another decline in the stock market or a financial crisis--it would spell serous trouble for homeowners with big new mortgages.</P>
<P>And any blow to consumer spending would knock another prop out from under the faltering recovery. The resulting scenario could include continued stagnation in economic growth because of the weakness in corporate investment, combined with falling living standards as consumers face a big debt burden.</P>
<P>That is what happened in Japan in the early 1990s. An economy that a few years before had been praised as a model for achieving sustained prosperity sank into a decade of stagnation that still hasn't ended.</P>
<P>Other possibilities include a replay of the weak economic recovery of the early 1990s--known at the time as the "joyless recovery" because it didn't produce better living standards for the vast majority of Americans. Or another financial crisis could send the economy reeling into the second dip of a recession.</P>
<P>Either way, workers will get the short end of the stick--asked to pay yet again for an economic mess that the bosses created.</P>

