<P><font face="Times New Roman, Times, serif" size="5"><b>Ashcroft's dirty bomb lie</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 23, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>JOHN ASHCROFT could hardly contain himself in June when he crowed to the world that authorities had arrested an alleged al-Qaeda operative who was plotting to set off a "dirty bomb" in Washington, D.C.</P>
<P>The supposed terrorist, Abdullah al-Muhajir--a U.S. citizen who changed his name from Jose Padilla--was thrown in a Navy prison and classified an "enemy combatant." That means he can be held and questioned indefinitely, without being charged or having access to a lawyer.</P>
<P>For days after the arrest, the airwaves were filled with "terrorism experts" speculating about the damage from a "dirty bomb"--a conventional explosive packed with radioactive material. But now, law enforcement officials are admitting that they have no evidence of a "dirty bomb" plot.</P>
<P>The case against al-Muhajir was "blown out of all proportion," one government official told <I>Newsweek </I>magazine. "If Padilla had any accomplices in the U.S., they have never been found--or even identified," said the magazine. In fact, <I>Newsweek</I> reported, "Inside the U.S. intelligence community &#133;there were high-level doubts about Ashcroft's dramatic announcement from the start." </P>
<P>But these "doubts" didn't stop the Feds from holding al-Muhajir in total isolation, stripping him of all his civil rights. It seems that the only people who are really looking "dirty" these days are the thugs at Ashcroft's Justice Department--who are ready to take away our rights without even a shred of evidence.</P>

