<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">The Bush gang spews hot air at its made-for-TV summit</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Clueless in Crawford</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 23, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH and his administration of corporate executives may be fans of the free market. But when it comes to the free discussion of ideas, they leave little to chance.</P>
<P>At last week's economic summit in Waco, Texas, the heavy-handed stage managing of the Bush team was visible at every turn. The forum brought together a cast of some 300 academics and corporate executives, along with a carefully selected representation of "real" people, to ponder the state of the economy.</P>
<P>"I'm one of the real people," crowed Phyllis Hill Slater, president of her own architectural firm in Floral Park, N.Y. So what's Phyllis' idea for getting the economy on track? Cut taxes, pass an energy reform bill and implement government-backed terrorism insurance.</P>
<P>Surprise, surprise--that's a carbon copy of Dubya's plans! The whole event was tightly scripted to keep the rest of the day's news--such as American Airlines' decision to lay off 7,000 workers and the diving stock market--from intruding on the proceedings.</P>
<P>"We heard a lot of really challenging ideas today," said Treasury Secretary Paul O'Neill. "We had several people in my session tell us that not only should we make the tax cuts permanent, but we ought to accelerate the ones that are delayed."</P>
<P>What kind of fools do they take us for? The only really "challenging" question is just how long the Bush administration intends to respond to every question about its strategy for the economy with the words "cut taxes."</P>
<P>The truth is that the lion's share of Bush's tax cut package passed last year will go to the wealthiest 1 percent of taxpayers. During the next 10 years, this superrich elite will enjoy an average tax cut of $342,000 each, draining $500 billion from the federal budget.</P>
<P>Now there's some money that could be put to better use than lining the pockets of the already obscenely wealthy. But you didn't hear even a whisper about reversing the Bush tax cut in Waco--or back in Washington, for that matter. </P>
<P>Even Democrats, who chided the White House for its fake forum, by and large avoided that suggestion--perhaps because they provided the margin of victory for the Bush tax cut in Congress.</P>
<P>It's not hard to figure out why the Bush administration would propose giving more money to the rich as a way to fix the economy. On the Friday after the Waco forum, Bush partied with his "Pioneers"--donors who raised $100,000 for the Bush-Cheney ticket--at his ranch in Crawford, Texas. The "real" people and TV cameras weren't invited--because this was a meeting of the people who actually count in terms of the Bush administration's agenda.</P>
<P>Bush may not be the sharpest tool in the shed, but when it comes to selling political influence to raise campaign cash for the Republicans, he's second to none.</P>
<P>We need to expose Bush's agenda of stealing from the poor to give to the rich. And we need to fight for a society organized around human need, not corporate greed.</P>

