<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Colombia's killers get green light</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 23, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE DIRTY war in Colombia just got dirtier, with a little help from the U.S. government. Under a provision in Washington's latest antiterrorism law, Colombia's new president, &Aacute;lvaro Uribe, can now use dozens of attack helicopters and other supplies provided by the U.S. in operations against left-wing rebels fighting a decades-old civil war. </P>
<P>Until this month, Congress had restricted the use of U.S. military and economic aid to anti-drug operations. The Pentagon had ways of getting around this. But now, Washington has gotten rid of even the window-dressing.</P>
<P>Under the new "relaxed" rules, the nearly $1.7 billion in U.S. military aid and hardware given to Colombia over the past two years can be used to directly attack left-wing guerrillas. "It will give us more mobility, much more capacity, much more firepower," Francisco Santos, Colombia's new vice president, bragged to reporters this week. </P>
<P>That's terrifying news for unionists, political activists and any ordinary person who happens to get in the way of the Colombian military--or the right-wing paramilitary butchers that the generals supply and sponsor.</P>
<P>Uribe, of course, wasted no time in taking advantage of this new opportunity. On August 12, the right-wing president--who has a history of ties to paramilitary groups and drug traffickers--declared a "state of domestic commotion." This allows the Colombian government to restrict movement, make arrests without warrants and recruit thousands of new military personnel, police officers and civilian informants.</P>
<P>"Although the State Department declined comment on the usefulness of the decree," the <I>New York Times</I> reported, "many officials in the Bush administration are sure to welcome the measure." Even if it means the Colombian military adds to its horrific record of human rights abuses.</P>
<P>But human rights have never been much of a concern for the U.S.--particularly when it comes to Colombia. Just to show where Washington's real priorities lie, the new antiterrorism law contains a provision to spend $6 million to protect an oil pipeline.</P>
<P>And to top it all off, the Bush administration last week formally asked Uribe to exempt U.S. military "trainers" in Colombia from prosecution by the International Criminal Court for any human rights abuses that may arise in connection with their work. </P>
<P>Demanding a promise of immunity for U.S. soldiers in return for billions in military aid--Washington's hypocrisy couldn't be more bald-faced. For all its talk about fighting the "war on drugs" and "protecting human rights," the only thing that the U.S. government cares about is protecting access to oil and cracking down on left-wing dissent.</P>
<P>That's the real agenda of Bush's "war on terror."</P>

