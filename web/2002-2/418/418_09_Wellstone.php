<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Don't blame Greens if Wellstone loses</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 23, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WITH CONTROL of Congress up for grabs in November, no state will be more closely watched than Minnesota. There, liberal Democratic Sen. Paul Wellstone faces a White House-engineered challenge from former St. Paul Mayor Norm Coleman.</P>
<P>Polls show Wellstone and Coleman running neck and neck, with the Green Party's Ed McGaa pulling single-digit support. Already, the liberal media's attack dogs are out in force, decrying the Green Party as a "spoiler."</P>
<P>Marc Cooper, of the <I>Nation</I> magazine, called the Greens' decision "a precipitous lunge toward political suicide." The Greens are deemed too sectarian/egotistical/unrealistic to see that they would be defeating "the most liberal, the most Green-ish member of the U.S. Senate," as Cooper described Wellstone.</P>
<P>Others on the left have pointed to McGaa's support for the war in Afghanistan as a reason to dump on him. But while this critique is correct, many of the same people slamming McGaa are willing to overlook Wellstone's support for the "war on terrorism" and his vote for the USA-PATRIOT Act.</P>
<P>Even if McGaa had perfect activist credentials, all of the same criticisms would be coming his way. The issue in this race isn't who the Green candidate is. It's about Wellstone--and whether supporting him or any other Democrat is the way to advance a politics that stands up for working people.</P>
<P>Wellstone won office in 1990 on a populist platform including support for health care for all and public financing of all elections. One of his first votes opposed President George Bush Sr.'s request for Congressional authorization of the 1991 war against Iraq. The <I>Nation</I> dubbed Wellstone the "Senator from the Left." But that was a long time ago.</P>
<P>After much prodding from the Clinton administration, he abandoned his position in favor of a government-run single-payer health care system to embrace Clinton's ill-fated pro-corporate system. So Wellstone jettisoned one of his main election issues in favor of "working within the system."</P>
<P>In the years since, Wellstone has been virtually invisible around any of the liberal causes that he's supposed to uphold. In fact, Wellstone's only time in the national spotlight occurred in 2000, when the Democrats trotted him out as the "soft cop" in their assault on Green Party presidential candidate Ralph Nader.</P>
<P>On issue after issue where his national position might have lent some attention to a progressive cause, he ducked out--when he wasn't caving in to the other side. For instance, he voted for the anti-gay Defense of Marriage Act in 1996 and has staunchly backed Israel.</P>
<P>With corporate scandals giving him a ready-made platform against Coleman--a blow-dried, corporate clone--Wellstone has had little to say. To counter Coleman's charges that he is a liberal ideologue, he has cast himself as a senator who works with Republicans to "get things done." And he's made time to welcome Sen. Joe Lieberman (D-Conn.)--one of the Democrats' most pro-corporate politicians--to headline a "Business Leaders for Wellstone" fundraiser.</P>
<P>As Wellstone morphed into a go-along-to-get-along Washington insider, he hasn't moved the national political debate an inch to the left. Minnesota journalist Steve Perry, writing in <I>CounterPunch,</I> nails it right on the head: "Yes, yes: Wellstone has done some admirable things in the Senate, Wellstone is palpably better than the inveterate lizard he's running against, blah blah blah. But if he continues to abstain from action and comment on the most pressing domestic matter of the day--he could, at minimum, lead the charge in demanding hearings regarding Bush/Harken and Cheney/Halliburton--he may still snatch defeat from the jaws of victory. And if that happens, he had better not whine about the White House or the Republican National Committee or the invisible Green candidacy of Ed McGaa, because he will have only himself to blame."</P>

