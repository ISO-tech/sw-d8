<P><font face="Times New Roman, Times, serif" size="5"><b>Exposing the CEO's lies at Delta Dental</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 23, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker, </P>
</B></I><P>I am one of the many unfortunate workers at Delta Dental who have been forced to strike.</P>
<P>The CEO of Delta Dental, Gary Radine, has not budged one inch from his unacceptable, original contract offer. He is reporting to the media that we have refused a contract with a 19 percent increase in wages, which makes us sound greedy and unreasonable. What he doesn't report is that after this "wage increase" is divided up, it comes out to 3.8 percent per year. </P>
<P>What he also is not reporting is that with his proposed increase in work hours with no overtime pay, increased medical and prescription co-payments, and reduced benefits overall, his so-called "19 percent increase" actually turns out to be a reduction in our overall earnings. </P>
<P>Mr. Radine has stated that we need to cut back "to remain competitive"--even though, according to a 2002 Department of Managed Care report, Delta Dental has the second-lowest operating cost ratio in the industry. </P>
<P>Just because we make less than $30,000 per year doesn't mean that we are stupid. I wonder what kinds of reductions management has had to take? Are limousines, lunches and quarterly bonuses for the management considered more necessary than medical insurance for the rest of us? </P>
<P>I might be more convinced by Mr. Radine's claim that "This is a great contract!" if the terms of the agreement also applied to him. Hey Gary, I've got a great idea! Why don't you try living on what we make? Maybe then, after you pull up in your limo to try to convince us of what a "great" contract it is, I'll listen.</P>
<P>Until then, you're smart enough to figure out what you can do with it.</P>
<B><P>A striking Delta Dental worker</P></B>

