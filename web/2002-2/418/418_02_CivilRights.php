<P><font face="Times New Roman, Times, serif" size="4">Feds' police-state methods exposed</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Ripping up civil rights</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 23, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ARBITRARY detentions. Violation of due process. Secret arrests. Illegal interrogations. The Bush administration is using police-state methods that would shame some of the world's cruelest dictators. That's the finding of a report by Human Rights Watch of Attorney General John Ashcroft's witch-hunt of immigrants following the September 11 attacks.</P>
<P>According to the 95-page report, the U.S. government detained more than 1,000 people during the investigation--some of them for months under restrictive conditions, including solitary confinement. The report also details allegations of physical and verbal abuse against detainees because of their national origin or religion. </P>
<P>Human Rights Watch researchers say that they aren't sure how many people are languishing in prisons without access to lawyers or to trial--because the Bush gang won't release any names. "The total number of persons detained in connection with the September 11 investigation may never be known," says the report. "The withholding of the identities of those charged with immigration violations in the context of the September 11 investigation--called 'special interest' cases in government documents--makes it impossible to check the accuracy of the numbers released by the Department of Justice, but there are indications that more people have been arrested than the government has recognized."</P>
<P>The report also details cases in which random encounters with police or even just neighbors' suspicions--based on no more than national origin and religion--led to people being interrogated about possible links to terrorism.</P>
<P>Meanwhile, the Justice Department flouted normal procedures to keep noncitizens in Immigration and Naturalization Service custody--on the off chance that they might be connected to terrorism. In other words, if you're an immigrant, you're guilty until proven innocent as far as John Ashcroft is concerned.</P>
<P>So many people have been detained, in fact, that according to a recent article in the <I>Village Voice,</I> some prisons are experiencing a boom in cash flow--as the federal government pays them to warehouse detainees in criminal facilities.</P>
<P>The abuse of detainees at the whim of the government is sickening--and shows the lengths that Ashcroft and his cronies will go to in order to carry out their war on our rights. "An immigration violation should not give the government license to rip up the rule book," said Jamie Fellner, director of Human Rights Watch's U.S. Program. "By restricting judicial oversight and blocking public scrutiny, the government has exercised virtually unchecked power over those it has detained."</P>

