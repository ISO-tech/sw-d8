<P><font face="Times New Roman, Times, serif" size="4">NYC's homeless crisis drives teen to suicide</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Victim of the shelter system</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Wengraf</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 23, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>JASON-ERIC WILSON is a victim of the disastrous state of New York City's social services. But Mayor Michael Bloomberg and his administration won't even admit that there's a problem.</P>
<P>On August 7, Wilson, a mentally ill 16-year-old, killed himself in his family's shelter room in Harlem. "My son committed suicide because we were being threatened with being sent back there," Eric Wilson, Jason-Eric's father, told the <I>New York Times</I>.</P>
<P>"There" is the Emergency Assistance Unit (EAU)--New York's only referral office for the homeless to find room in a shelter, located in a poor, Bronx neighborhood. New York law requires that homeless New Yorkers be given space in a shelter, but many people end up waiting at the EAU for an assignment until well after midnight.</P>
<P>"I never saw anything like it in my life: babies lying on the floor, even newborns," said Carmen Garcia, an unemployed security guard who spent a week at the EAU with her husband and four children, according to the <I>Times</I>. "We filled out papers. They told us to wait, and we waited."</P>
<P>Lynn Louis, an organizer with Picture the Homeless who does advocacy work at the EAU, says that "the conditions in these overnight placements are horrendous." "They all need to be cleaned and made sanitary," she told <I>Socialist Worker</I>. "People aren't told their rights at the shelters, there's no bilingual information posted, no staff training, no grievance procedure, and treatment by the case workers is completely arbitrary." This is what Jason-Eric feared going back to.</P>
<P>When the Wilson family tried to get shelter space at the beginning of August, a city nurse listed the father's leukemia and Jason-Eric's psychiatric history on a screening form so that they would be given priority. But they still waited until 2 a.m. before they were taken to a shelter in Harlem.</P>
<P>And the ordeal didn't end there--the city threatened to evict the Wilsons from the shelter and take away food stamps benefits because Eric Wilson couldn't prove that he had custody of his own son. That's when Jason-Eric took his own life.</P>
<P>The suicide forced the homeless crisis--which activists say is getting worse --into the news. But Linda Gibbs, Bloomberg's commissioner of homeless services, insisted that there's "no crisis." </P>
<P>In fact, the city's outrageous response to the suicide was to convert a local jail into a shelter for families with children. "Even in the darkest days of the last administration, when there were many draconian things that were proposed and some implemented, the idea of children in prison was never, never an issue," said Steve Banks, a lawyer at the Legal Aid Society.</P>
<P>Bloomberg has promised more "affordable housing" in New York City. But this is a smokescreen. "Affordable housing," as defined by federal housing requirements, is way beyond the reach of the poor, and even many low-wage workers.</P>
<P>"There's a lack of willingness to own up to the problems," Louis said. "There's no big commitment to address the root causes of homelessness--for welfare recipients, but also for low-wage workers."</P>
<P>Jason-Eric Wilson's suicide is the only latest evidence of the sick priorities of this system. We can't let them get away with this.</P>
