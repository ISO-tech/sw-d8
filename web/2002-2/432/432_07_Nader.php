<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Are Nader voters to blame for Bush?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 6, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH is president, he's pushing a bloody new war on Iraq and an assault on civil liberties and union rights at home. And if you voted for Ralph Nader, it's your fault. That old complaint from liberals writing in magazines like the <I>Nation</I> showed up once again last month following the trouncing of Democrats in midterm elections.</P>
<P>After the 2000 election, Nader was blamed for "stealing" votes from Al Gore, allowing Bush to grab the White House. Now, this argument is coming from a few people who themselves campaigned for Nader--like Ronnie Dugger, founder of the Alliance for Democracy, who introduced Nader at the Green Party nominating conventions in 1996 and 2000.</P>
<P>In an article in the <I>Nation</I> titled "Ralph, Don't Run," Dugger insists, "We cannot afford another division in our ranks that will bring about the election of George W. Bush in 2004." He warns that a Nader campaign in 2004 "could well darken this entire period in U.S. and world history."</P>
<P>Is this true? First of all, it's worth recalling what the 2000 Nader campaign was really about. Running as an alternative to what he called "two apparently distinct political entities that feed at the same corporate trough," Nader gave expression to people's anger with corporate greed and the lack of choice in the U.S. political system.</P>
<P>His campaign raised demands for a $10 an hour minimum wage and universal health care. It gave an electoral voice to the growing global justice movement, and it thrived because of its grassroots approach, staging huge rallies and encouraging activism.</P>
<P>To say that Nader "took votes away" from Al Gore assumes that the 2.7 million ballots cast for Nader were Gore's to begin with. Of course, Dugger wants something better than Gore in 2004. He proposes that progressives challenge the conservative wing of the party. The argument is that we can "take back" the Democratic Party.</P>
<P>But history shows that it wasn't "ours" to begin with. While it poses as the "party of the people," the Democrats are a party of war, of the Jim Crow South and of attacks on workers. Democrats John F. Kennedy and Lyndon Johnson, not Republicans, escalated U.S. involvement in the Vietnam War. Democrat Jimmy Carter, not a Republican, used Taft-Hartley against striking miners in 1978--like Democrat Harry Truman before him. And at every opportunity, Democrats have done their best to co-opt social movements to further their own interests.</P>
<P>For every "moderate" Democrat like Al Gore and Joe Lieberman, there are at least as many "progressive" Democrats providing cover for the party's real face. At the 1988 Democratic convention--as he delivered his primary supporters to loser presidential nominee Michael Dukakis--Rev. Jesse Jackson declared: "Left wing. Right wing&#133;It takes two wings to fly. Whether you're a hawk or a dove, you're just a bird living in the same environment, in the same world."</P>
<P>We need a completely different alternative to the Democrats--and that has to be built. In the beginning stages, any genuine alternative to the two mainstream parties will get a minority of votes--and therefore will face all the same arguments from those urging a vote for the Democrats as a "lesser evil."</P>
<P>The campaign for Nader was a step toward that alternative--but only a step, because Nader all but disappeared after the election. The post-election movement that Nader talked about mobilizing during the campaign could have made a difference in building an opposition to Bush's theft of the White House--and now his bloody new war on Iraq.</P>
<P>An independent alternative won't come out of thin air. It will be connected to grassroots struggle--which history shows is the way that real political change is made. And this is why the job of stopping Bush starts right now--at demonstrations to oppose his rotten policies, not by playing the scare game on the pages of the Nation.</P>

