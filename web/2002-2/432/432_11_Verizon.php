<P><font face="Times New Roman, Times, serif" size="4">In NYC, 10,000 workers rally to demand&#133;</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>No layoffs at Verizon!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By a CWA shop steward</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 6, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Over 10,000 union members at Verizon rallied in front of the damaged phone building at Ground Zero November 21 to protest potential layoffs. The high turnout reflected the severity of the layoff threat, as well as the recognition that this is an attack on every member, no matter how senior. "Once they get away with one layoff, that's it, it won't stop," one Brooklyn technician told <I>Socialist Worker</I>.</P>
<P>The union has never allowed layoffs at Verizon or its predecessor companies in New York State. The Communications Workers of America (CWA) organized the rally to highlight the company's hypocrisy in laying off some of the recovery workers it had praised as "heroes" only months before. </P>
<P>Verizon has some debt issues, but made $2.1 billion last quarter. Unlike some industries that are operating well below capacity, Verizon has too little capacity. Rather than build, the company is letting the infrastructure rot. </P>
<P>Despite the mounting service problems, Verizon would rather take advantage of the crisis in telecommunications and the anti-labor climate created by the Bush administration to break the power of the union. Verizon has also proposed an early contract extension, demanding concessions like a pay freeze and no job security for any worker with less than 10 years. </P>
<P>Most members believe a strike is the only thing that could stop Verizon, but union leaders haven't called a strike authorization vote.</P>
<P>Verizon claims that a strike during the contract would be illegal. But action is the only thing the employers understand. CWA union activists should take up the leadership's call for a work-to-rule campaign and coordinate action whenever possible.</P>
<P>We need to petition the union leadership to tell them that we're ready to strike. Any action that unites people in taking on management will bring us that much closer to stopping layoffs and defending our union.</P>
<I><P>CWA activists should email ranknfileverizon@hotmail.com for more information.</P>

