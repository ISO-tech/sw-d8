<P><font face="Times New Roman, Times, serif" size="5"><b><I>Frida</I>'s glimpse into a revolutionary life</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Elizabeth Lalasz</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 6, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>MOVIES: <I>Frida,</B></I> directed by Julie Taymor, starring Salma Hayek and Alfred Molina.</P></font>
<font face="Times New Roman, Times, serif" size="3"><I><P>FRIDA, </I>A new movie about Mexican artist Frida Kahlo, is a visually stunning portrait of the painter, which also gives a glimpse into the importance of revolutionary politics in her life.</P>
<P>Kahlo lived from 1907 to1954, a time of incredible political upheaval and revolutions in Mexico and around the world. At the university in Mexico City, Kahlo had many close friends who were student activists.</P>
<P>She joined the Young Communist League, the youth wing of the Mexican Communist Party (CP), and met her future husband, Diego Rivera, the famous muralist, who was a founding member of the CP in Mexico.</P>
<I><P>Frida</I> gives an idea of what it was like to be a revolutionary at this time. In an early scene, Kahlo observes a heated debate between Rivera and artist David Siqueiros, in which Rivera argues in support of Russian revolutionary Leon Trotsky, who was fleeing for his life because of his opposition to Stalin. </P>
<P>Another scene depicts Kahlo in New York City during a well-known controversy in which her then-husband Rivera refused to remove a depiction of the Bolshevik leader Lenin from a public mural commissioned for John D. Rockefeller Jr.</P>
<P>A later scene shows a dinner conversation with Trotsky about the rise of fascism in Europe. The movie also does a good job depicting Kahlo's romantic relationships with women, which were very controversial at the time, as a normal part of her life.</P>
<P>But <I>Frida</I> only really touches on the influence revolutionary politics had on Kahlo's life and art, leaving you wanting to know more. To learn more, pick up the book <I>Frida</I> by Hayden Herrera.</P>

