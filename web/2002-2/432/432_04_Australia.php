<P><font face="Times New Roman, Times, serif" size="5"><b>Australians rally against the WTO</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 6, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>More than 1,500 protesters defied government bans on marches for three days running to protest the World Trade Organization (WTO) meetings taking place in Sydney, Australia, November 14-15. </P>
<P>The meetings were of 25 trade ministers and were part of the "informal" discussions where the ruling class of rich countries like Australia and the United States stitch up agreements over how they are going to maximize the profits of their corporations at our expense.</P>
<P>The New South Wales police minister refused to grant any march permits and spent weeks denouncing protest organizations and threatening protesters with dire consequences if they defied police. He even indicated that special anti-terrorism legislation passed for the Sydney 2000 Olympics would be invoked to prevent serious protests from taking place.</P>
<P>Despite the media and police barrage, protesters came out in support of refugee rights on Wednesday night, and police backed down. On Thursday, larger numbers came out against the war and against the right of the WTO to decide the fate of billions. The demonstration wound its way through downtown Sydney, disrupting traffic, burning U.S. and Australian flags in protest against the war and protesting at a number of corporate venues. </P>
<P>On Friday, protesters headed to Homebush--the site of the WTO meeting--and despite police, the march successfully reached the five-kilometer ring of steel surrounding the conference venue. As the demonstrations attempted to breach the fences, major struggles with the police broke out, resulting in almost 40 arrests. </P>
<P>After continuing at the fences for several hours, protesters began to march back to the train stations. When police attempted to surround and break up the march, the demonstrators managed to break past the police lines with no further arrests.</P>
<P>Protesters left vowing to keep up the fight against corporate rule and confident that another world is very definitely possible.</P>
<B><P>Marc Newman,</B> Sydney, Australia</P>

