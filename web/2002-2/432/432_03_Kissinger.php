<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Henry Kissinger's cover-up commission</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 6, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>AS CONGRESS was about to adjourn last month, the Bush administration gave in to demands for an independent commission to study the September 11 attacks. The White House had one final condition, which Congress accepted--that Bush get to appoint the chair of the panel.</P>
<P>Now we know why. Bush is counting on Henry Kissinger to protect his administration in the inquiry into what top officials knew about the attacks before they happened.</P>
<P>He picked the right man for the job. The former secretary of state and gadfly global schemer is a proven liar and utterly reliable defender of power and privilege--not to mention a war criminal responsible for death and mayhem around the globe, from Chile to East Timor to Vietnam and Cambodia.</P>
<P>"Who better to ferret out government duplicity and manipulation than the man who engineered secret wars, secret bombings, secret wiretaps and secret coups, and still ended up as a Pillar of the Establishment and Nobel Peace Prize winner?" wrote <I>New York Times</I> columnist Maureen Dowd in an uncharacteristically scathing article. "Now Mr. Bush can let the commission proceed, secure in the knowledge that Mr. Kissinger has never shed light on a single dark corner, or failed to flatter a boss, in his entire celebrated career&#133;If you want to get to the bottom of something, you don't appoint Henry Kissinger. If you want to keep others from getting to the bottom of something, you appoint Henry Kissinger."</P>
<P>It's no surprise that the White House resisted an investigation of September 11. The attacks gave the administration the best excuse imaginable for promoting its agenda of expanding U.S. power abroad and clamping down at home. Ordinary people who support Bush's wars out of concern for "security" might have second thoughts if they knew just how little the White House did to respond to threats before September 11.</P>
<P>The "Kissinger Commission" might shed more light on the administration's blunders, but don't expect much. Both Republicans and Democrats on the panel are certain to focus on new anti-terrorism measures to shred more civil liberties.</P>
<P>Meanwhile, the questions that should be asked won't be. After all, why is the United States hated around the world? The answer isn't religion, or a "clash of civilizations," as mainstream commentators put it--and even less Bush's balderdash about terrorists who "hate our freedoms."</P>
<P>The U.S. is hated because of the terrible crimes carried out around the world by the U.S. government. A commission that studied this would ask why the Bush White House is planning a war against Iraq to wipe out a nonexistent "threat" to the U.S.--at the cost of yet more death and destruction for ordinary Iraqis.</P>
<P>It would ask why Ronald Reagan called Osama bin Laden and other Islamists "freedom fighters" back in the 1980s, when the U.S. was arming and training them to fight a civil war against the ex-USSR in Afghanistan.</P>
<P>It would look at how the policies of Corporate America and the U.S. government cause suffering and humiliation in every corner of the globe.</P>
<P>No anti-terrorism measure will change the fact that these policies stoke hatred and violence against the U.S. Inevitably, this has "blown back" in attacks like the September 11 hijackings--where the victims weren't those responsible for the policies, but ordinary people with little knowledge and no control over what is done in their name.</P>
<P>Washington is using the "war on terrorism" to justify the use of terrible violence around the globe--just as its main Middle East ally, Israel, will exploit last week's attack on an Israeli hotel in the Kenyan city of Mombassa to justify its war on Palestinians.</P>
<P>We have to cut through the lies--and expose the truth about a brutal system that fuels hatred and desperation.</P>

