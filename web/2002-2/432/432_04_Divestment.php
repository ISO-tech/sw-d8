<P><font face="Times New Roman, Times, serif" size="4">Divestment movement comes under attack</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Fight Israel's apartheid</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 6, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P></B>
<P>At Columbia University, a petition demanding that the university divest from companies that sell arms to Israel has received more than 570 signatories, including 99 from faculty. </P>
<P>The petition specifically targets Boeing and General Electric (makers of Apache helicopter gunships), Caterpillar (maker of the D9 bulldozer used by the Israeli army to knock down Palestinian homes), and Lockheed Martin Corporation (maker of the F-15 and F-16 fighter jets). </P>
<P>The movement to force universities to divest from Israel follows a similar movement from the 1980s--the struggle against apartheid South Africa. In April, a rally organized by Students for Justice in Palestine had more than 400 attendees, and in November, organizers of the divestment petition held a teach-in that drew more than 150 people. </P>
<P>Unfortunately, while the petition has gained wide support, it also has been met with official opposition and condemnation. Both Columbia University President Lee Bollinger and Barnard College head Judith Shapiro have issued statements attacking the petition and supporting a pro-Israel counter-petition--which is backed by some of Columbia's richest alumni and donors, like New England businessman Robert Kraft.</P>
<P>President Bollinger writes in his statement that the comparison between Israel today and South Africa during apartheid is "both grotesque and offensive." If he really believes this, then he disagrees with many of those who led the fight against apartheid in South Africa. </P>
<P>For example, Archbishop Desmond Tutu has often made the comparison, writing "Yesterday's township dwellers can tell you about today's life in the occupied Palestinian territories. To travel only a few blocks in his own homeland, an elderly grandfather waits to beg for the whim of a teenage soldier. More than an emergency is required to get to a hospital; less than a crime earns a trip to jail&#133;The indignities, dependence and anger are all too familiar."</P>
<P>The movement to force Columbia to divest is part of a broader fight against the U.S. government's $5 billion-a-year support for Israel's apartheid. Bollinger's accusation that the divestment movement is anti-Semitic is a false charge and masks the reality of the situation--that Israel is a colonial-settler state built on the oppression of the Palestinians. </P>
<P>The divestment campaign can help broaden and sustain our movement against this oppression, and we should support it.</P>
<B><P>Jonah Birch,</B> New York City</P>

