<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Why the White House won't tolerate anything except war</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The Bush doctrine of mass destruction</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 6, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE CLOCK is ticking for a U.S. assault on Iraq. December 8 is Iraq's deadline for presenting United Nations (UN) weapons inspectors with "complete and full disclosure" of all past and present programs for producing "weapons of mass destruction." </P>
<P>With this declaration in hand, U.S. officials will get down to the brass tacks of figuring out how to declare Iraq in "material breach" of the UN Security Council's resolution--and provide a trigger for war.</P>
<P>No one can predict when the assault will begin, but whatever the exact timeline, there can't be any doubt that war is on the agenda. Many of the military pieces are already in place. Next week, Gen. Tommy Franks--commander of the U.S. war on Afghanistan--will travel to the Gulf state of Qatar for the first-ever command-and-control Pentagon exercise conducted outside the U.S.</P>
<P>Franks and 750 members of his staff will use Qatar's As Sayliyah base--the likely command headquarters for a war on Iraq--to conduct a test with top generals and commanders of the Army, Marines, Air Force, Navy and Special Operations forces. With this headquarters set up, "all we have to do is hop on a plane" to run the war on Iraq, as one officer put it.</P>
<P>But the larger blueprint that the U.S. is following has been under development for years by the hawks who now dominate the Bush administration. Daniel Goure--a military analyst at the Lexington Institute and close adviser to Deputy Defense Secretary Paul Wolfowitz--is positively giddy about the prospect of war. "The combination of U.S. air and ground power is truly unstoppable," Goure told <I>New Yorker</I> journalist Nicholas Lemann. "This is it--the Roman legion, the German panzers. Truly unstoppable. My belief is that the administration is making this move because it will be a shock wave, a ten on the Richter scale, in its effect on that region. Let's win the war and let it play out."</P>
<P>And what will this "shock wave" involve?</P>
<P>The use of "thermobaric" bombs that use intense heat and pressure to destroy underground bunkers--the kind of bomb that killed more than 400 people in a Baghdad air raid shelter during the first Gulf War. A massive carpet bombing that will hammer Iraq's already devastated infrastructure, plunging millions of already impoverished Iraqis into even more desperate conditions. Turmoil unleashed throughout the Middle East--and the stage set for a U.S. war on any country that steps out of line next.</P>
<P>That's the vision of the future set out in the White House's National Security Strategy document--known as the "Bush Doctrine"--with its plans for "pre-emptive wars" to destroy any challenge to Washington's superpower status.</P>
<P>In the face of this, no antiwar activist can afford to take a wait-and-see approach to the UN weapons inspections. The need to strategize, organize and mobilize is urgent. We need to broaden the opposition to the Bush war drive in every city and on every campus. And we need to start building now for the largest possible turnout at national antiwar demonstrations in Washington, D.C., and San Francisco on January 18. We have to stand up to the Bush doctrine of mass destruction.</P>

