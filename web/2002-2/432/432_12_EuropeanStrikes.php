<P><font face="Times New Roman, Times, serif" size="4">A wave of strikes</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Unions on the march in Europe</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 6, 2002 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>A WAVE of union struggles is rolling across Europe as workers stand up to employer attacks. Leading the way are public-sector unions fighting privatization and seeking decent pay increases.</P>
<P>In France, the conservative government of Prime Minister Jean-Pierre Raffarin was under fire last month when tens of thousands of striking government workers took to the streets of Paris. Truck drivers blocked roads. Air traffic controllers walked out, grounding airplanes. Nurses, postal workers and others joined the action. The actions were reminiscent of the public-sector general strike of 1995, which defeated a previous right-wing government's privatization plan.</P>
<P>Meanwhile, in Italy, workers at Fiat auto plants blocked roads in a protest against plans to slash jobs in preparation for a buyout by General Motors. Twenty thousand workers marched in Rome against layoffs--on the heels of two general strikes and a mass protest against the right-wing government's anti-union legislation.</P>
<P>It isn't surprising that unions would mobilize against right-wingers like Italian Prime Minister Silvio Berlusconi. Yet even in Britain, unions are taking on "their" Labour Party government--run by Prime Minister Tony Blair. Recent weeks have seen strikes by government workers and rail and subway workers.</P>
<P>But the key battle was last month's 48-hour strike by the Fire Brigades Union (FBU). The firefighters are seeking a 40 percent pay increase. That would only bring top pay to $48,000 a year. But Blair is insisting that the raise be limited to just 16 percent--and that the union accept "modernization" of work practices, a code word for a speedup in the pace of work. The FBU--with the support of the Trades Union Congress, the British equivalent of the AFL-CIO--has vowed to fight on, striking again if necessary.</P>
<P>Other battles are looming. In Germany, the largest union, Verdi, is threatening walkouts this month in an effort to win a 3 percent wage hike. Even in Poland, the model for free-market policies in Eastern Europe, some 10,000 coal miners marched in Katowice against plans to close mines. And in Portugal, unions called a general strike for December 10 to protest government plans to roll back labor laws won in the 1974 revolution that overthrew a dictatorship.</P>
<P>Winning these struggles will require some of the biggest mobilizations that European labor has seen in decades. But the battles that have broken out already show the potential for unions to fight.</P>

