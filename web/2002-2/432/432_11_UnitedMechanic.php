<P><font face="Times New Roman, Times, serif" size="4">United Airlines pushes mechanics to vote for cuts--or else</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"Now's the time to fight"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 6, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AFTER STUNNING both management and union officials by rejecting concessions, mechanics at United Airlines were being forced by union leaders to vote on essentially the same terrible deal, as <I>Socialist Worker</I> went to press.</P>
<P>Members of the International Association of Machinists (IAM) in District 141-M voted November 27 by 57 percent to 43 percent to reject $700 million in concessions--part of a proposed $5.2 billion in givebacks by United's unions.</P>
<P>Ramp workers, who are also members of the IAM, voted to accept concessions, as did flight attendants shortly afterwards. Pilots had earlier agreed to pay cuts.</P>
<P>But since each union agreed to accept givebacks only if all other unions did so, the rejection by the 13,000 mechanics could derail the entire package. In that case, United says it will file for bankruptcy--and ask a judge to void labor contracts and impose even deeper cuts.</P>
<P>Cracking the whip is the federal Airline Transportation Stability Board, which is demanding labor concessions as a precondition for guaranteeing loans made to United. To pressure mechanics to take the cuts, Scotty Ford--the IAM mechanics' leader at United who recently got a pay raise of his own--is working hand-in-glove with United's new CEO, Glenn Tilton, who got a $3 million "signing bonus" to take over the airline.</P>
<P>In a letter to union members dated December 2, Ford announced that IAM mechanics would vote on a revised concessions deal just three days later. But the only difference was that mechanics would have a choice of which of four vacation days that would go unpaid in the years 2004 through 2007.</P>
<P>The new offer also contains a promise by Tilton to discuss "quality of work-life issues"--but doesn't commit management to change the hated schedule that forces mechanics to work six days straight with no overtime pay.</P>
<P>And the agreement still contains a clause that would allow United to void the deal in bankruptcy court anyway--"in the event of a war in Iraq or a sudden, unforeseen event that substantially disrupts air travel (e.g., an act of God, act or threat of terrorism, etc.)."</P>
<P>"This would allow the company to do anything they want until 2008," said Jennifer Biddle, a shop steward in IAM Local 1781 at United's maintenance facility in San Francisco. "The company is saying that we expect to cut losses in half by 2003, and be in the black by 2004, but they want us to sign a contract that will cost $60,000 over the next six years. Basically that's giving them a free year."</P>
<P>Biddle was one of a number of union activists in San Francisco and San Diego who organized an informational picket against concessions November 21, shortly before the concessionary deal was announced.</P>
<P>"Since negotiations began two years ago, we've gone from the United Airlines with a friendly face to one that's invoking [court-ordered] Temporary Restraining Orders against us and firing people illegally for doing their jobs," Biddle told <I>Socialist Worker</I>. "This has been a long process that people have been going through. Anyone with any kind of illusions in the company has had those illusions shattered."</P>
<P>Once the "no" vote was in, IAM officials didn't meet with rank-and-file mechanics to hear their concerns, but went behind closed doors with management instead.</P>
<P>United bosses and IAM leaders hope that they can intimidate mechanics into accepting the cuts. "The District 141-M Executive Board strongly recommends ratification, as it is the final opportunity to avoid bankruptcy and protect against the elimination of our entire collective bargaining agreement," union leaders wrote.</P>
<P>But a growing number of IAM mechanics feel that they have nothing left to lose. "The message of the 'no' vote is that we've had enough," Biddle said. "We didn't benefit from the economic boom. For us, what's happening now is more of the same of what we experienced in the 1990s. Enough is enough. Now is the time to fight."</P>

