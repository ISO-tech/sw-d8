<P><font face="Times New Roman, Times, serif" size="5"><b>Far from the "ideal" American family</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 6, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>MOVIES: <I>Far From Heaven,</B></I> written and directed by Todd Haynes, starring Julianne Moore, Dennis Quaid and Dennis Haysbert.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>EVERY YEAR for the holidays, advertisers and the media trot out sugar-coated images of the "perfect family." Of course, these imaginary families bear more resemblance to 1950s TV shows than they do to any real-life family. But as the excellent new movie <I>Far From Heaven</I> shows, the ideal of the perfect family is a horrible burden to have to live up to--no matter what decade you're in.</P>
<P>As the movie opens, Frank and Kathleen Whitaker appear to be living the American Dream with their two young children in Hartford, Conn., in 1957. Kathleen, in her immaculately tailored dresses, is the model 1950s housewife--the perfect complement to her business executive husband and a paragon of family, civic and social virtue.</P>
<P>But just beneath the surface of Frank and Kathleen's perfect world, things are going terribly wrong. That's because Frank happens to be gay. In 1957, however, Frank has few options to express his sexuality. When he enters an underground gay bar, he feels like--and indeed at the time was--a criminal.</P>
<P>Kathleen walks in on him and a male lover, and their marriage starts to unravel. Kathleen begs Frank to see a doctor, and he agrees, telling her that he despises himself and wants to "lick this problem"--which was considered a psychological disease at the time.</P>
<P>The immense tension wears on the couple--as Frank's rage and pain and Kathleen's desperation becoming increasingly apparent. These scenes are the most moving of the film, as Frank struggles to make his wife understand but can't find the words.</P>
<P>And as Kathleen finds comfort in her friendship with her Black gardener Raymond Deagan, he exposes her to an entirely new world--the Black working-class world that white suburbanites don't even realize exists.</P>
<P>Director Todd Haynes shows that just beneath the surface of "progressive" Hartford lies an uglier reality--where white suburbanites make crass jokes about integration in front of their all-but-invisible Black domestic workers.</P>
<P>The movie makes it clear that in the idealized world of 1957, there can be no happy endings for Raymond, Kathleen or Frank. The movie is as impressive for its attention to detail as it is for its story--from the amazing set design to the vintage-sounding score by Elmer Bernstein.</P>
<P>The movie was inspired by period Douglas Sirk melodramas like <I>All That Heaven Allows,</I> about a middle-aged woman and her affection for her younger gardener, and <I>Imitation of Life,</I> about a rich woman whose maid's daughter passes for white.</P>
<P>But <I>Far From Heaven</I> is so successful because it offers a challenge to the stereotype of the "perfect" 1950s American family while resisting the temptation to filter the story through the lens of modern morals. The film shows just how confining the image of the perfect family was--and how devastating the consequences could be for anyone that dared to challenge that convention.</P>

