<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Should we blame the "Israel lobby"?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 12, 2002 | Page 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>IT'S NO secret that the United States is the most powerful and important backer of the state of Israel. The most common explanation for U.S. support is that the "Israel lobby" exercises huge influence in pressuring the American government.</P>
<P>Sometimes, explanations like this come from those who claim that "the Jews" have the U.S. government wrapped around their fingers. We can reject this as crude anti-Semitism.</P>
<P>But that said, there is a powerful network of Zionist organizations--led by the American Israel Public Affairs Committee (AIPAC)--that donates money to pro-Israel candidates and lobbies the U.S. government on behalf of Israel. There's nothing anti-Semitic about pointing this out--especially since pro-Israel organizations aren't shy about touting their own influence. Just last month, they claimed credit for defeating Rep. Earl Hilliard (D-Ala.), one of the few critics of Israel in Congress, in his Democratic primary race.</P>
<P>But are these organizations and their lobbying efforts the reason why the U.S. supports Israel? From a socialist point of view, the answer is no.</P>
<P>Israel annually receives more than $3 billion in U.S. aid. Egypt runs second at around $2 billion. Yet no one would seriously claim that the aid that Egypt receives is the result of an "Egyptian lobby."</P>
<P>What's more, about 99 percent of the nearly $83 billion that the U.S. has given to Israel came after 1967. During the first 19 years of Israel's existence, from 1948 to 1967, the U.S. supported it. But Israel didn't become the central lynchpin of U.S. policy in the Middle East until after it proved its worth by smashing the Egyptian, Jordanian and Syrian armies during the 1967 Six Day War. Ironically, Richard Nixon, an open anti-Semite, was the president who oversaw the initial buildup of U.S. support for Israel.</P>
<P>It's no coincidence that Israel and Egypt are the two top recipients of U.S. aid. Both are important allies in the region where the lion's share of the world's oil is located.</P>
<P>Since the end of the Second World War, the U.S. has tied its "national security" to its access to and control of the flow of oil. That's why the U.S. has given military and economic aid to prop up "friendly" states in the region--not only Israel, but Egypt, Jordan and the Gulf monarchies, too.</P>
<P>The U.S. puts Israel at the top of the list because its government and population form the only uniformly pro-U.S. state in the region. In countries like Egypt, pro-Western governments rule over restive populations that hate the U.S. government's support for Israel and for their own oppressive regimes.</P>
<P>The U.S. attempt to reconcile support for Israel with support for conservative Arab regimes sometimes leads to conflicts. After the 1991 Gulf War, for example, the Bush Sr. administration withheld U.S. loan guarantees to strong-arm the Israeli government into participating in the U.S.-sponsored Madrid "peace talks." When one White House political adviser warned against the Bush administration's stand, Secretary of State James Baker is alleged to have said, "Fuck the Jews. They don't vote for us anyway."</P>
<P>Today, veterans of both the Bush Sr. administration and the early 1990s Israeli government are working hand-in-glove. What changed? Not the strength of the "Israel lobby," but the policy of the U.S. government.</P>
<P>In the early 1990s, the U.S., under both Bush and Clinton, wanted to push "the peace process" as its plan for "regional stability." Today's Bush administration has abandoned that strategy for now.</P>
<P>The U.S. government--for its own imperial reasons--decides how much leeway Israel has. And this leeway defines how successful the "Israel lobby" will be.</P>
<P>As long as Israel remains central to U.S. imperialism in the Middle East, Israel will continue to receive U.S. backing and aid. That's why Israel's ace in the hole in Washington isn't AIPAC--but the Pentagon, the CIA and the military-industrial complex.</P>

