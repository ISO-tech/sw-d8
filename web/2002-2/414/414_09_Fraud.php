<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The fraud at the heart of capitalism</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 12, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IT'S UNLIKELY that MSNBC producers read <I>Socialist Worker</I> before they came up with a graphic to appear on-screen behind their talking heads. "Corporate Crime Wave" it read, mimicking a recent <I>SW</I> headline.</P>
<P>When even mainstream news sources are forced to tell it like it is, you know something is really rotten. After all, these were many of the same people--like MSNBC's sister financial network, CNBC--who made their bones hyping the late 1990s stock market.</P>
<P>In league with crooked CEOs and accountants and bought-and-paid-for financial analysts, financial journalists continued to hype overpriced stocks even as the insiders were planning to bail out of them.</P>
<P>Satisfying as it may be to see WorldCom's Bernie Ebbers or Enron's Ken Lay fall from their perches, we can't lose sight of the real economic dynamics at work. Financial scandals always accompany the collapse of speculative manias endemic to capitalism. When Internet startups were valued at the same levels as long-established manufacturers, the bull market was clearly unsustainable.</P>
<P>Karl Marx, analyzing the bursting of the first modern speculative bubble in England in the 1840s, described how financial transactions created "fictitious capital" whose prices bore increasingly little relation to underlying value. "Stock was underwritten to the limits of possibility, i.e. as far as there was money to cover the initial payments," Marx wrote in <I>Capital,</I> Vol. III. "As for the rest, a way would be found! Why let a splendid opportunity pass? Why not get into the swing of it?"</P>
<P>Anticipating today's doctoring of accounts, Marx describes how manufacturers engaged in fraud to boost their balance sheets. "The poorer [manufacturers] became, the greater the need they had to purchase, in order to make up, by new advances, the capital they lost on the past adventures. Purchases thus became, not a question of supply and demand, but the most important part of the finance operations of a firm laboring under difficulties."</P>
<P>For someone whose ideas history is supposed to have refuted, Marx comes closer to explaining how Enron and the rest worked than <I>Business Week</I> or <I>Fortune</I>.</P>
<P>Because the entire system of capitalism is based on profit that bosses gain from paying workers less than the value their labor imparts to the products the bosses sell, organized theft lies at its heart. If theft lies at the core of the system, it's not hard to see how desperate capitalists can resort to the open theft that WorldCom and Enron became famous for.</P>
<P>The "corporate accountability" reforms the government is proposing won't address these problems. They are meant to bolster "investor confidence"--to save capitalism from itself in the hopes of staving off a flight of foreign capital from U.S. markets.</P>
<P>The capitalists who buy politicians in Washington want to make sure that any reforms will "restore confidence." At the same time, they want to make sure those reforms don't actually limit their freedom to make as much profit as they can.</P>
<P>So Republicans and Democrats will talk tough, but do little. After all, with their bipartisan support of the 1996 Telecommunications Act and efforts to gut regulation of the financial industry, they helped create the environment in which the Ebbers and Lays could commit their crimes.</P>
<P>And you can be sure that corporate criminals won't be forced to pay anywhere near what they owe workers whose lives they destroyed.</P>
<P>Corporate criminality will continue to punish ordinary people in ways that aren't even apparent today. Californians will be paying for decades in higher taxes and worse public services for the billions in ill-gotten gains Gov. Gray Davis handed over to power pirates like Enron.</P>
<P>The recent scandals aren't aberrations. They are the way capitalism really works. They are the products of a system that fleeces the many to line the pockets of the few.</P>

