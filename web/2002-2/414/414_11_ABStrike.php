<P><font face="Times New Roman, Times, serif" size="5"><b>R.I. Teamsters' strike hits beer distributor</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Matthew Taibi, Teamsters Local 251</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 12, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>PROVIDENCE, R.I.--Almost 50 Teamsters at the Rhode Island Anheuser-Busch (A-B) distributor McLaughlin &amp; Moran went on strike at midnight July 1 for higher wages and respect.</P>
<P>The drivers, drivers' helpers and warehouse workers--members of Teamsters Local 251--are still recovering from a six-year deal that left them behind the wage levels of other liquor workers in the state. Drivers make $16.05 an hour and helpers make $15.42 an hour, plus health care and pension benefits.</P>
<P>Teamsters Local 251 holds much of the bargaining power, as McLaughlin &amp; Moran is the sole A-B distributor in Rhode Island, and the contract expiration date of July 1 is peak time for beer distribution.</P>
<P>The two other major distributors in Rhode Island, Centrex and Wayne, are both covered under collective bargaining with Local 251. Strikers have been leafleting customers to support the union workers and to help bring about a speedy settlement.</P>
<P>Management has vowed to start running scab operations starting the week of July 8. Strikers are planning ambulatory picketing of scab trucks and picketing of businesses that accept scab deliveries.</P>
<P>The key to victory is building solidarity with the rest of the 5,000-plus members of Local 251, as well as the rest of the Rhode Island labor movement. Such solidarity will allow the strikers to prevent scab deliveries, keep morale up and force management to show the respect the workers deserve by providing a good contract.</P>

