<P><font face="Times New Roman, Times, serif" size="4">This whole system is crooked</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Den of thieves</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 12, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE DIRECTOR of an energy company makes obscene profits by selling stock he knows will tank.</P>
<P>Sound familiar? On any given day, you could expect to find this story about another all-star CEO exposed for committing fraud, cooking the books and assorted double-dealing.</P>
<P>This time, though, the corporate criminal is George W. Bush.</P>
<P>In June 1990, Bush--then a director at Harken Energy--dumped his shares in the company for a profit of nearly $1 million. Two months later, Harken's stock price plummeted by 40 percent. Seems that the company had understated its losses a bit--about $9 million worth.</P>
<P>Today, Bush is getting heat for not filing a document about the stock sale with the Securities and Exchange Commission until 34 weeks later--an oversight that his flacks have brushed off as a "mix-up by company lawyers." And he has the nerve to talk about cleaning up corporate crime!</P>
<P>Bush and his handlers were preparing a big speech to "restore faith in the integrity of American business" as <I>Socialist Worker</I> went to press.</P>
<P>According to the carefully orchestrated campaign of leaks from "top administration officials," Richie Rich plans to tell people how much he understands our anger at companies whose dirty deals have wrecked the lives of tens of thousands of workers. He might even propose that crooked executives go to jail.</P>
<P>That's a bit much coming from a man who's surrounded by them. Like Dick Cheney--who has so far dodged a congressional inquiry into his role as the brains behind the White House's "Enron says" energy policy.</P>
<P>Cheney jumped to the White House from his former position as CEO of the energy services company Halliburton, which is under investigation for booking more than $100 million of disputed costs as revenues.</P>
<P>Then there's Secretary of the Army Thomas White, a former Enron executive who helped to organize the multibillion-dollar rip-off of California during the state's "energy crisis."</P>
<P>And when the thieves in the White House aren't committing their own corporate crimes, they're shilling for the WorldComs and Rite Aids as these tarnished stars of Corporate America rob workers' futures.</P>
<P>Because it's not just a few cheaters who are ruining the free-market system. The whole system is built on cheating--cheating working people out of a decent living and their hopes for the future, all for the sake of profit.</P>
<P>We can do much better--by fighting for a socialist alternative.</P>

