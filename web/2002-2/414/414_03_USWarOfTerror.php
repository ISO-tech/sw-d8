<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">U.S. slaughter in an Afghan village exposes the reality</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Bush's war of terror</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 12, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>A WAR against terror or a war of terror? That's the question that comes to mind when you hear how the latest victims of the U.S. war on Afghanistan died--dozens of people, mainly women and children, torn to pieces by U.S. bombs for the "crime" of being at a wedding celebration.</P>
<P>Kakrakai, a village in central Afghanistan, was attacked in the early morning hours of July 1 after U.S. warplanes claim to have come under "anti-aircraft fire." In reality, a few guests at a wedding party fired guns into the air, say survivors.</P>
<P>But the Pentagon won't even admit that it made a mistake. "We do not attack our allies," snarled Gen. Dan McNeill, the commander of coalition forces in Afghanistan.</P>
<P>So why did U.S. Special Forces soldiers storm the homes of villagers afterward, stopping people from helping their wounded relatives? "Until 7 or 8 o'clock in the morning, the Americans didn't allow anyone to help the injured and to cover the bodies," said Mohammad Anwar, whose brother Sharif was the host of the wedding celebration. "Most of their clothes had been burnt off [in the bombing]. They kept filming and photographing the naked women."</P>
<P>Heaping insult on top of injury is the fact that Kakrakai was an anti-Taliban stronghold during the U.S. air war last year. In October, Sharif had protected Hamid Karzai--now the interim president of Afghanistan--during his U.S.-backed covert mission into central Afghanistan. Now Sharif is dead, along with his wife, four of his children and approximately two dozen other relatives--a victim of the very forces that Sharif had welcomed.</P>
<P>This is the "new Afghanistan"--where this month a top government minister was assassinated, almost certainly by a rival among the gang of feuding warlords who now run the country, with the blessing of the U.S.</P>
<P>And George W. Bush has the gall to talk about bringing "democracy" to Afghanistan! What Washington really cares about is imposing U.S. interests around the globe, no matter who gets in the way.</P>
<P>The war on terror is a convenient cover story--not only for justifying its naked use of overwhelming military force against anyone who dares to oppose it, but in silencing opposition to its policies at home. That fact was highlighted last week by yet another security scare during the July 4 holiday.</P>
<P>Naturally, the media spent hours of frantic coverage on the shootings apparently committed by an Egyptian man, Hesham Mohamed Hadayet, at Los Angeles International Airport. Federal authorities unanimously insisted that there was no evidence of any link to terrorism. But that didn't stop the newly created Transport Security Administration from unveiling a plan to hire thousands of armed guards to patrol terminals at U.S. airports.</P>
<P>Any guesses as to how soon after they're deployed will come the first reports of young Arab men harassed by gun-toting goons? </P>
<P>Yet what went largely unsaid amid the uproar about the LAX shootings was that another terrorism scare passed without incident. This has been the pattern since September 11--waves of vague alerts, often clearly timed to distract attention from other issues in danger of bumping the war on terrorism out of the spotlight.</P>
<P>Washington's incredible hypocrisy is leading to growing questions. <I>Washington Post</I> columnist David Broder--the very personification of the establishment media--concluded in a recent article that opinion polls showing sustained support for the "war on terrorism" are misleading.</P>
<P>He quoted an audience member at a recent panel discussion who asked why Broder kept referring to strong support for Bush's war, "because I don't know anyone here who favors it."</P>
<P>That's the experience of growing numbers of people. This questioning hasn't yet translated into increased antiwar activism. But at each new step of the way--from preparations for an invasion of Iraq to the Bush gang's power grabs carried out in the name of "security"--Washington sows more doubts.</P>
<P>This represents a new audience for the thousands of people who opposed Bush's war from the beginning--and the potential for rebuilding a stronger antiwar movement as Washington marches on, at home and abroad.</P>

