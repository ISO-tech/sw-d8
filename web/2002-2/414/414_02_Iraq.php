<P><font face="Times New Roman, Times, serif" size="4">Pentagon's invasion plan is "pretty far along"</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Preparing more horrors for Iraq</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 12, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>U.S. PLANS for another full-scale assault on Iraq are at an advanced stage, according to press reports last week.</P>
<P>After giving the CIA the green light in June to "remove" Saddam Hussein--meaning to assassinate him--the Bush administration has the Pentagon at work planning more horrors for a country that the U.S. military reduced to a "pre-industrial state" during the 1991 Gulf War. "Right now, we're at the stage of conceptual thinking and brainstorming," a Pentagon official told <I>the New York Times</I>. "We're pretty far along."</P>
<P>According to the <I>Times,</I> the current plan calls for an invasion involving 250,000 marines and other soldiers. Some news reports claimed that U.S. military "advisers" and Special Forces troops had already been deployed in Jordan to lay the groundwork for a future attack.</P>
<P>The <I>Times</I> report said that the ground invasion would be preceded by waves of air assaults by U.S. warplanes based in as many as eight nearby countries. In fact, the air force has been stockpiling weapons, ammunition and spare parts at bases in both the U.S. and the Middle East. "We don't know when or where the next contingency will be, but we want to fill up the stock bins," said Gen. Lester Lyles.</P>
<P>In other words, the question isn't if the U.S. will find an excuse to attack, but when.</P>
<P>One possible justification is Iraq's refusal to allow United Nations (UN) weapons inspectors back into Iraq. UN General Secretary Kofi Annan failed to get Iraq's agreement to this proposal at talks on July 5--because he refused to provide assurances that devastating UN sanctions would finally be lifted or that the U.S. wouldn't be allowed to use inspectors as spies, as it has in the past.</P>
<P>But even if Annan comes up with an agreement, the U.S. government isn't likely to put off its war plans. The Bush gang wants to make it clear that it will organize a "regime change" in any country in the world that steps out of line.</P>
<P>Iraq has been reduced to one of the poorest countries in the world by more than a decade of war and sanctions. "Until May of 2002, the total value of all food, medicines, education, sanitation, agricultural and infrastructure supplies that have arrived in Iraq has amounted to $175 per person a year, or less than 49 cents a day," wrote Hans von Sponeck, the former UN humanitarian coordinator for Iraq who quit in protest. "This has made postwar reconstruction impossible and ensured mass unemployment and continuing deterioration of schools, health centers and transportation."</P>
<P>Washington's war makers are preparing to impose even more horrors on the Iraqi people.</P>

