<p><a name="pledge"></a><font face="Times New Roman, Times, serif" size="4"><b>A pledge for our side to take</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>I recently published this pledge of allegiance in the student paper at the University of Texas. I have been receiving a great deal of hate mail over it, but I stand by it:</P>
<P>"I pledge allegiance to all the ordinary people in this country and around the world, to laid-off Enron and WorldCom workers, the maquiladora workers and sweatshop workers from New York to Indonesia, who labor not under God but under the heel of multinational corporations; I pledge allegiance to the people of Iraq, Palestine and Afghanistan, and to their struggles to survive and resist slavery to corporate greed, brutal wars against their families and the economic and environmental ruin wrought by global capitalism; I pledge allegiance to building a better world where human needs are met with real liberty, equality and justice for all."</P>
<B><P>Dana Cloud,</B> Austin, Texas</P>

