<p><a name="G-8"></a><font face="Times New Roman, Times, serif" size="4"><b>Bush's hypocrisy at the G-8 summit</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>At the recent G-8 summit in Canada, George Bush laid out his "vision" for a Middle East peace process and lectured the Palestinians, saying: "The status quo is simply unacceptable, and it should be unacceptable to them. They live in a--you know, they've been pawns in the game of peace, they have no hope, their economy is in shambles, they live in squalor. Their leadership has let them down."</P>
<P>Does the Idiot Son know he's also talking about the U.S.? We are all pawns in Bush's game of peace--which, in "Bush-speak," means endless war--in which the U.S. has stated its willingness to "strike first" for "peace." </P>
<P>Thousands of workers at WorldCom and Enron whose companies are "in shambles" "have no hope" of recovering their retirement plans. Millions of Americans "live in squalor" in the richest country in the world.</P>
<P>For once, I agree with that idiot. The status quo is simply unacceptable--and that's why we have to build struggles against Bush, the corporations and the government that protects them.</P>
<B><P>Pham Binh,</B> New York City</P>

