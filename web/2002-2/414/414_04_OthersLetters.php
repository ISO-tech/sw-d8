<P><font face="Times New Roman, Times, serif" size="4"><b>LA's health care nightmare</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>The health care situation in Los Angeles is frightening, with only more devastation in sight. There are around 3 million people without health insurance in Los Angeles County, insurance costs are rising between 20 and 25 percent and emergency rooms are often so crowded that ambulances are sometimes held up to five hours before the patient they are transporting is admitted. To make matters worse, Los Angeles County faces a projected $688 million budget deficit for 2005. </P>
<P>One solution that is being proposed is cutting the number of public hospital beds in half--converting three of the six county hospitals into clinics and eliminating 8,000 jobs. This is considered the most optimistic proposal. One man that I was speaking with who lives in South Central Los Angeles put it quite clearly: "People are going to die."</P>
<P>But while people in South Central are talking about how hospital closings are going to effect them, show-business celebrities are doing just fine. Dick Clark, for example, recently purchased a $15 million Malibu home. </P>
<P>Are there so few resources in Los Angeles County that we have to let people die? Does Dick Clark add more value to society than the millions of people that are served by county health care?</P>
<P>There is one proposal that we are unlikely to see from the system: Tax the rich.</P>
<B><P>Dasha Haas,</B> Los Angeles</P>

