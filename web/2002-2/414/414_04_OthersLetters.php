<p><a name="Somos"></a><font face="Times New Roman, Times, serif" size="4"><b>"Somos uno! We are one!"</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>I wanted to let your readers know about a historic event organized by antiwar activists here in San Diego, Calif., and Tijuana, Mexico. On Friday, June 21, we held a concert called "Somos Uno (We Are One)--Stand in Unity Against War!" on the border between the United States and Mexico at two oceanfront parks that are separated by a 10-foot high steel-mesh fence. </P>
<P>Several hundred people attended on both sides of the border to hear a dozen bands and several activists speak out against Bush's war and domestic agenda. Border-related issues, such as the proposal to build a third tier of fence and recent harassment of trolley passengers by the Border Patrol, were especially prominent.</P>
<P>Bands alternated performing 30-minute sets back and forth as the crowd, shouting, "Somos Uno!" moved to enjoy each act. </P>
<P>The concert wasn't a huge event, but it was a success because it reunited a good number of seasoned activists and drew in new organizers. Furthermore, the spectators were excited to be at an event that challenged the logic of the border and Bush's war. Many were open to radical politics, and <I>SW</I>'s sister newspaper <I>Obrero Socialista</I> now gets a small, but important, hearing among Tijuana activists.</P>
<P>Having a concert like this at a time when activism is at a low point was a little bit risky, but, because of its style, it appealed to lots of people. Hopefully, some of the attendees will become consistent activists. </P>
<P>"Somos Uno" opened a lot of doors for us down here, and we hope that <I>SW</I> readers will be able to gain some inspiration from it.</P>
<B><P>Chuck Stemke,</B> San Diego</P>

