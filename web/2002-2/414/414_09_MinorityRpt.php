<P><font face="Times New Roman, Times, serif" size="5"><b>Crime and technology in a future world</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Alana Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 12, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>MOVIES: <I>Minority Report,</B></I> directed by Steven Spielberg, based on a story by Philip K. Dick, starring Tom Cruise.</P>
</font>
<font face="Times New Roman, Times, serif" size="3"><P>THE SUMMER blockbuster <I>Minority Report</I> centers on science-fiction writer Philip K. Dick's story about a future world in which police arrest criminals before crimes have been committed. It's like a nightmare version of Attorney General John Ashcroft's America, where people are imprisoned on the basis of crimes they might commit.</P>
<P>Guilt is based on information obtained from "precognitives" who visualize future crimes. Tom Cruise plays John Anderton, the chief of the "Pre-Crime Unit" who flees after he's fingered for a future murder. He seeks to clear his name by acquiring a "minority report," documentation of cases in which the precogs disagree.</P>
<P>While <I>Minority Report</I> is unspectacular in many ways, what comes through is a clear vision of class disparity 50 years into the future. My favorite scene illustrates the position of the working class in this technologically enhanced future world. And it acknowledges the liberties that have been given up in favor of heightened security.</P>
<P>The police are hunting for Anderton in "the sprawl," the poorest part of the city where futuristic technology is absent, in contrast to the sleek, rich technological areas where Anderton works and lives.</P>
<P>Cops release high-tech surveillance machines shaped like spiders that crawl through the building, doing retinal scans of every person. Children are terrified as the creatures crawl up their bodies. Perhaps even more unsettling is a couple who are so used to this surveillance tactic that they stop arguing just long enough for the spiders to scan them, and then go back to fighting.</P>
<P>The technological advances that make this a science-fiction film are limited to the world of the wealthy, as these innovations do not touch the lives of the poor, except when they're under surveillance. Audiences can't help but recognize how much <I>Minority Report</I> mirrors conditions today.</P>

