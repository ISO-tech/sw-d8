<P><font face="Times New Roman, Times, serif" size="4">Behind the AIDS nightmare:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Drug bosses profit while millions die</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 12, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>HIV INFECTION rates and deaths from AIDS are skyrocketing worldwide. And the blame lies squarely with the U.S. and other governments in the advanced world.</P>
<P>That's the verdict of new research released this month in the run-up to the international AIDS conference in Barcelona, Spain, on July 7-12. "The scale of the AIDS crisis now outstrips even the worst-case scenarios of a decade ago," concludes a study from UNAIDS, a joint United Nations (UN) program on HIV/AIDS.</P>
<P>Worldwide, 40 million people are HIV-positive--and every day, 6,000 more teens and young adults under the age of 25 are infected. Unless current trends are reversed, over the next 20 years, 70 million people will die, according to the UN.</P>
<P>But beneath these horrifying statistics lies an even uglier truth. The spread of the epidemic could have been halted or substantially slowed if wealthy nations like the U.S. had committed more resources to fighting HIV/AIDS around the world.</P>
<P>And though pharmaceutical companies have developed new treatments that extend the lives of sufferers, only a miniscule fraction of those living with HIV/AIDS in sub-Saharan Africa receive these life-saving drugs.</P>
<P>The epicenter of the epidemic is now plainly in sub-Saharan Africa. In Botswana, for example, 39 percent of adults are now HIV-positive, and life expectancy is predicted to drop to 27 years.</P>
<P>The figures are not much better in other African countries. According to estimates, the average life expectancy of babies born in 11 African countries will be little more than 30 years in the near future. And in countries in southern Africa, as many as 50 percent of new mothers will die from the disease. </P>
<P>"In less than 10 years from now, we project life expectancy will be back to levels that have not been seen since the end of the 19th century," Karen Stanecki, chief of the health studies branch of the U.S. Census Bureau, told Britain's <I>Guardian</I> newspaper.</P>
<P>Meanwhile, the crisis has spread in recent years, with China, India and Russia all experiencing steep rises in the number of infections. Even in the U.S., researchers say that the rate of HIV infection increased by 10 percent from 1998 to 2000, mainly due to a lack of education about safer-sex practices.</P>
<P>What makes this all the more appalling is that the disease could be slowed dramatically if even a fraction of the resources available to the world's most powerful governments were used. According to the British medical journal <I>The Lancet,</I> a stepped-up intervention--involving mass media campaigns, public-sector condom promotion and distribution, voluntary counseling and testing programs, and needle-exchange programs for drug users--could prevent 29 million cases of HIV infection worldwide by the year 2010.</P>
<P>The full cost of the effort over the next decade: $27 billion. That's less than one-fourteenth of the U.S. military budget for next year. But Washington's plans for new aid packages, while announced with a flurry of rhetoric, are an insult.</P>
<P>Meanwhile, the giant drug companies are doing their part to make sure that AIDS sufferers die needlessly in huge numbers. Of the 30 million people suffering from HIV/AIDS in sub-Saharan Africa today, only 30,000 receive the cutting-edge anti-retroviral drugs that could keep them alive.</P>
<P>Worldwide, only 1.6 million people with HIV--out of 40 million--have access to these medications. So the drugs exist, but they don't get to those who need them because the pharmaceutical companies have to protect their profits--this is the disgusting reality of a system that puts the wealth and power of a minority before saving lives.</P>

