<font face="Times New Roman, Times, serif" size="5"><b>Toronto city workers fight privatization</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Brian Erway</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 12, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEARLY 22,000 public workers are on strike in Toronto, Ontario, in the largest municipal strike in Canadian history. </P>
<P>Garbage has been piling up on the streets of Toronto since June 26, when 7,000 outside workers represented by Canadian Union of Public Employees (CUPE) Local 416 struck against Mayor Mel Lastman's plans for privatization.</P>
<P>A week later, 15,000 inside workers from CUPE Local 79 walked out. They include workers on ferries to the Toronto Islands, nurses at public health facilities, day-care workers, parks and recreation workers, museum staff, social services workers and clerical and computer staff in all city departments.</P>
<P>The push to privatize public services has come late to Toronto, Canada's largest city, and unions are trying to hold the line against subcontracting. The city is offering guarantees that full-time unionized workers with 10 years of seniority would have lifetime employment if their job is taken over by an outside contractor. But this would "not only cost city workers their jobs," Ontario CUPE President Sid Ryan wrote in the <I>Toronto Star</I>. "It also means the city would create a two-tier workforce. One with good wages, benefits, seniority and protection, and another with low wages and constant job insecurity, as the city privatizes more services."</P>
<P>Amid the stench of rotting trash, Mayor "Smell" Lastman warned reporters to expect a long strike. "To be perfectly frank with you, I can't see them coming back before September or October," he said. But Lastman and friends may be hoping to goad the Ontario provincial parliament into legislating strikers back to work--under the excuse of the "threat to public health."</P>
<P>"It has become clear that the politicians in charge of these negotiations don't want to negotiate," said CUPE Local 79 President Ann Dembinski. "This dispute isn't about money&#133;The city is trying to divide and rule." </P>
<P>Picket lines at the garbage transfer stations remain spirited as the trash continues to pile up. Toronto city workers have drawn the line--and set an example for the labor movement with their determination to stand up to privatization.</P>

