<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Will Argentina's crisis drag down the whole continent?</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Revolt against the free market</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 12, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>AN EDITORIAL in the <I>New York Times</I> last week took George W. Bush to task for his administration's "let them eat cake attitude" towards Argentina. The editorial warned of "an emerging political contagion--a dangerous backlash against free markets and economic liberalization--spreading throughout Latin America."</P>
<P>But don't think that the <I>Times</I> is worried about the fate of ordinary Argentinians. It's worried that the country's wrenching social crisis could spread throughout Latin America, causing trouble for the U.S. in every corner of its "backyard."</P>
<P>The Bush administration's insistence on continued budget cuts--conveyed by its mouthpieces at the International Monetary Fund (IMF)--is driving the Argentine economy deeper into a 1930s-style depression. In what was once the richest country in Latin America, the economy shrank by 16 percent during the first months of 2002.</P>
<P>Aside from the terrible human suffering, Argentina's free fall could drag its neighbors down with it. Latin America's largest economy, Brazil, recently sank into recession, and the country's currency has declined massively in value.</P>
<P>The economic slowdown here means that the U.S. Treasury Department would have a hard time stepping in and plugging holes, as it did during the 1998 financial crisis--potentially opening the floodgates to a generalized depression in Latin America with dire consequences for the U.S. economy.</P>
<P>Meanwhile, the pressure of the crisis is leading to growing struggles of workers and the poor. An uprising from below reversed the U.S.-backed coup in Venezuela this April, public-sector and oil workers strikes continue in Colombia despite savage repression, and mass protests in June beat back the Peruvian government's plan to privatize electricity.</P>
<P>In Brazil, Luiz "Lula" In&aacute;cio da Silva, the leader of the left-wing Workers Party, is 15 points ahead in opinion polls for the presidential election slated for October 6. Lula himself may be prepared to protect the status quo--for instance, he now supports continuing to pay Brazil's debts to the IMF--but his election would raise workers' expectations.</P>
<P>Meanwhile, the last three weeks have seen dramatic developments in Argentina's upheaval. On June 26, police killed two protesters and wounded more than 100 as part of a "get-tough" plan pushed by Argentina's "president-select" Eduardo Duhalde. Two mass protests of 40,000 and a national day of action set for July 9 forced Duhalde to fire the Buenos Aires police chief and move up presidential elections by six months to March 2003.</P>
<P>But Argentina's rulers will keep trying to re-impose their control. In an open bid at intimidation, Duhalde's current foreign minister, Carlos Ruckhauf, recently recalled that he was the minister in the mid-1970s who signed into law the bill that became the basis of Argentina's "dirty war" and "campaign against terrorism." More than 30,000 leftist militants were killed or "disappeared" in the "dirty war."</P>
<P>For the time being, Ruckhauf's threats have been pushed back. But in a crisis as deep as this one, the status quo is impossible. One side or the other will have to assert itself. The challenge for workers across the region is to organize themselves to win that battle.</P>

