<P><font face="Times New Roman, Times, serif" size="4">Judge's ruling leaves an innocent man on death row</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"Justice didn't prevail"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 12, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"AN INNOCENT man could be executed because all these people are preventing him from getting a new trial to protect their careers." That's what Robin Milan-Hobley told reporters after a Cook County judge refused to grant her brother Madison Hobley--an innocent man on Illinois death row--a new trial.</P>
<P>"Justice didn't prevail in this courtroom," Robin told <I>Socialist Worker</I>. "It's a corrupt system&#133;How do you raise your children to do the right thing when the system won't do the right thing?"</P>
<P>Madison is one of the Death Row 10--a group of men who were beaten and tortured by cops working for Chicago Police Commander Jon Burge. In 1987, Madison was convicted of setting a fire that claimed the lives of seven people, including his wife and infant son. But the state's so-called "evidence" is full of holes.</P>
<P>Cops claim that Madison used a gas can to start the fire and that he confessed to tossing the can down a second-floor hallway. Yet a detective sent to the building to look for the can claims to have found it under a sink behind a locked door. Lab results show that Madison's fingerprints weren't on the gas can, which showed no fire damage despite being "found" in the gutted remains of the apartment building.</P>
<P>Madison faced horrific abuse at the hands of Chicago cops. He was handcuffed to a wall, kicked, beaten and suffocated with a plastic typewriter bag until he passed out, as officers tried to make him confess.</P>
<P>Despite police claims that he "confessed," Madison never signed a confession, and no written or taped confession was ever produced at his trial. Why? Because, police say, their notes "got wet."</P>
<P>This absurd claim would be laughable--but for the fact that an innocent man's life is on the line.</P>
<P>Activists had hoped that Madison would finally win a new trial. Instead, the judge denied every one of his petitions.</P>
<P>Madison's family and anti-death penalty activists are planning to protest this grotesque injustice. The Campaign to End the Death Penalty has scheduled a July 10 rally to protest at the offices of Cook County State's Attorney Richard Devine.</P>
<P>We can't let this rotten injustice system get away with keeping Madison Hobley on death row!</P>

