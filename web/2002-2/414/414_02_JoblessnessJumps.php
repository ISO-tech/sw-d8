<P><font face="Times New Roman, Times, serif" size="4">Joblessness jumps again</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Is the recovery on the ropes?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 12, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WILL THE jobless recovery give way to a new recession? That was the fear of many mainstream economists following reports that unemployment in June rose to 5.9 percent despite six months of economic growth.</P>
<P>What's more, Corporate America's financial scandals have contributed to a big drop on the stock market and a decline in the value of the dollar relative to other currencies, raising the possibility of a financial crisis as well.</P>
<P>"Try hapless recovery or nervous recovery or hidden recovery or reversible recovery or frail recovery or moderating recovery or anemic recovery or easily short-circuited recovery," wrote <I>New York Times</I> reporter Louis Uchitelle. "Each of these descriptions of an uncertain economy has its constituents."</P>
<P>While the U.S. economy grew at an estimated 6.1 percent annual rate in the first quarter of this year, "more than half the growth came from businesses increasing production because they emptied warehouses and cut inventories so much last year," the <I>Wall Street Journal</I> reported.</P>
<P>Consumers, stuck with big debts and faced with uncertain job prospects, held back on spending. As a result, growth in the second quarter is expected to be much slower.</P>
<P>One exception to the consumer spending figures is car purchases, with buyers taking advantage of "zero-percent" financing deals. But these deals simply accelerate sales that would have taken place in the future--and in the meantime, they cut into auto industry profits.</P>
<P>The other big boost to the economy is housing, as millions of homeowners take advantage of low interest rates to refinance mortgages. But property values have soared--creating a bubble in housing that's replaced the one that was punctured in the stock market.</P>
<P>"Mortgage payments are running as high as 42 percent of income--way up from their normal range of 25 percent to 30 percent," the <I>Journal </I>noted this month. That puts millions at risk of losing their homes in the event that they are laid off.</P>
<P>This is no small risk. The economy lost 1.8 million jobs between March 2001 and April of this year. But what worries the bosses most is that profits--the lifeblood of capitalism--have barely recovered from last year's recession lows. Lousy profits--along with record levels of corporate debt --have discouraged business from making the kind of capital investments that spurred the 1990s boom.</P>
<P>Now corporate fraud is leading investors to dump stocks. Many of those pulling out are foreign investors, who poured billions into the U.S. during the boom years--allowing the U.S. economy to consume more than it produced.</P>
<P>As dollar-based investments are sold off, the value of the dollar is dropping--which could result in price inflation. Yet if the Federal Reserve Bank responds by boosting interest rates to head off inflation, it would likely choke off economic growth. "Hence the perfect storm," economist and columnist Robert Kuttner wrote. "Recession, inflation and a plunging stock market."</P>
<P>There's no predicting exactly where the economy will go next. But it's already clear that the bosses who shut workers out of the "miracle economy" of the 1990s have only misery to offer today.</P>

