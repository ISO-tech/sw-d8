<P><font face="Times New Roman, Times, serif" size="5"><b>Unearthing racism in the Sunshine State</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Anthony Arnove</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 12, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>MOVIES: <I>Sunshine State,</B></I> written and directed by John Sayles, starring Angela Bassett, Edie Falco and Bill Cobbs.</P>
</font>
<font face="Times New Roman, Times, serif" size="3"><P>AMERICAN independent movie making doesn't get any better than John Sayles. Sayles's movies explore how capitalism buries (sometimes literally) the history of its violence against exploited and oppressed people. They show how ordinary people struggle to make sense out of and take control of their lives.</P>
<P>If you haven't seen <I>Matewan,</I> about West Virginia coal miners fighting to form a union, or <I>Lone Star,</I> you should rent them. But don't miss the chance to see Sayles's new film <I>Sunshine State</I> in the theaters while you can.</P>
<I><P>Sunshine State</I> is a sharp satire and drama about a Florida community being torn apart by the pressures of real-estate developers and corporate tourism. The area had once been the home of one of the only beaches where Blacks could go during the era of Jim Crow, Lincoln Beach. (Sayles based the story in part on the fate of the historic American Beach.)</P>
<P>But now little remains of the Black community. A group of white businessmen are cynically using a washed-up former football star who is Black, Flash Phillips, to buy out the locals who still own property there.</P>
<P>Edie Falco, from HBO's <I>The Sopranos,</I> gives an amazing performance as Marly, who manages her father's old hotel and restaurant that is coveted by a real-estate company. Angela Bassett plays Desiree, who left Lincoln Beach when she became pregnant as a teenager and returns, uncertain of what she will find.</P>
<P>Sayles brilliantly interweaves the lives of his Black and white characters. And he never loses his comic touch. During the town's "Annual Buccaneer Days"--a pathetic attempt to generate tourism--Francine, a local civic booster, presides over a Disney-like whitewashing of area history.</P>
<P>When the event is a total bust, she complains that the real history of Florida is about "mass murder, rape, slavery." "Don't people realize how difficult it is to invent a tradition?" she protests.</P>
<P>Meanwhile, someone all too aware of the town's real tradition, Dr. Lloyd, struggles to revive the political consciousness of the Black community. Throughout the film, he organizes whoever he can to oppose the developers and the local politicians who are taking kickbacks to let them in.</P>
<P>Sayles contrasts Dr. Lloyd's effort to organize with Flash Phillips's resignation. When Desiree confronts Flash about what he's doing, he tells her, "Life moves on, shit gets bought out and sold. There's a handful of people who run the whole deal and there's the rest of us who do what they say and get paid for it."</P>
<P>But <I>Sunshine State</I> shows that this is not the whole story. The past isn't always so easy to bury, as we find out in a comic twist off fate near the end of the movie. And even if they don't always win, people resist.</P>

