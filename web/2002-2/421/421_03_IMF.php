<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Taking on the global loan sharks</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 13, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>SECRETARY OF State Colin Powell felt the heat last week when protesters heckled his speech, with chants of "Shame on Bush!" on the final day of the Earth Summit in Johannesburg, South Africa.</P>
<P>Powell was visibly flustered at the interruption in an address proclaiming Washington's "serious" approach to ending world poverty. Washington is serious all right--but not about committing money and resources to helping poor countries.</P>
<P>What the U.S. is serious about is making the poor pay--and pay and pay. Any promise of U.S. aid comes with calls for more austerity, as plotted out by Washington's friends at the International Monetary Fund (IMF) and World Bank.</P>
<P>In Argentina, once the richest society in South America, the IMF's neoliberal prescriptions have translated into slashed living standards and rocketing unemployment. But the crisis has also produced mass resistance--including an uprising that ousted the government last December. Last month, Argentine public-sector workers struck for 48 hours and joined with the unemployed for new protests.</P>
<P>Here in the U.S., activists will have their own chance to send a message to the global loan sharks--when the IMF and World Bank hold their annual meetings in Washington, D.C., on September 28-29.</P>
<P>Last year, the movement for global justice was set back after September 11, as the Bush administration launched its "war on terrorism." But since then, those fighting against IMF and World Bank policies around the world have increasingly made the connections between U.S. economic policies that punish poor countries and U.S. military actions that target them.</P>
<P>This month's protests will allow activists to take up these same issues. This will be a perfect opportunity to tell George W. Bush: We're sick of your war on the world's poor--whether it means starving the people of Argentina or raining bombs on Iraq.</P>
