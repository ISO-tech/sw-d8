<P><font face="Arial, Helvetica, sans-serif" size="2"><b>GERMANY</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Can Schr&ouml;der come from behind?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar and Jeff Bale</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 13, 2002 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>GERMAN CHANCELLOR Gerhard Schr&ouml;der is trying to win re-election in a close race by opposing both a U.S. war on Iraq and American-style corporate greed.</P>
<P>"I'm against a military intervention--clearly, without conditions," including United Nations approval, Schr&ouml;der said September 8 in a televised debate with his conservative opponent Edmund Stoiber.</P>
<P>Schr&ouml;der, the leader of the Social Democratic Party (SPD), is also criticizing the free-market policies that led to suffering for U.S. workers. "Bankruptcies, the plundering of the small man in the U.S&#133;is not the German way that we want for our people," he said last month. "This land&#133;will remain strong when there is social justice at its core."</P>
<P>This swing to the left is a departure for Schr&ouml;der, who has pursued moderate pro-business policies modeled after those of Bill Clinton and British Prime Minister Tony Blair. His program involved big tax cuts geared to business and the middle class. And more recently, he backed plans to force the unemployed to take temp jobs to qualify for benefits.</P>
<P>Schr&ouml;der promised that such measures would reduce Germany's unemployment. But with the economy growing at just 0.3 percent per quarter since 1996, joblessness increased to 4 million.</P>
<P>Earlier this year, members of the powerful IG Metall union carried out selective strikes for wage increases--and much of their anger was directed at Schr&ouml;der's government.</P>
<P>In local elections last April in the impoverished state of Saxony Anhalt in the former East Germany, the Social Democrats' vote collapsed to 20 percent--a lower total than the ex-Communist PDS party.</P>
<P>Electoral defeats of center-left governments in Italy, France, Portugal and the Netherlands led to the widespread belief that Schr&ouml;der's day was done. Stoiber, the governor of Bavaria and leader of the Christian Social Union/Christian Democratic Union (CDU), has led in opinion polls for most of the year.</P>
<P>A hard right-winger known for immigrant bashing and fanatical opposition to abortion, Stoiber decided to campaign as a "compassionate conservative" to pick up working-class votes. The result is that the formal programs of the candidates are fairly similar, with Stoiber actually promising more aid for laid-off workers.</P>
<P>But after last month's devastating floods, Schr&ouml;der declared that the business-oriented tax cut would have to be canceled in order to pay for aid and reconstruction--a move that was popular among working-class voters and the left. Also, aware of growing opposition to the U.S. war on Iraq, he took an increasingly harder line against George W. Bush.</P>
<P>Though a victory for him would shake up the European left parties' infatuation with pro-market, pro-war policies, no one should believe that Schr&ouml;der is a pacifist. He and his foreign minister, Joschka Fischer of the Green Party, enthusiastically backed the NATO war on Serbia--the first use of German combat troops since the fall of Hitler. German troops are also among the international peacekeeping forces in Afghanistan, and German law enforcement has been rounding up suspected "terrorists" at the behest of the U.S.</P>
<P>But now Schr&ouml;der is denouncing the planned U.S. war on Iraq as an "adventure" to tap the widespread antiwar mood. Another factor in the election is the far right, which is trying to exploit anti-immigrant sentiment to gain votes. And the weak economy is creating a severe budget crisis that will confront the next government.</P>
<P>Whatever the outcome, the campaign points to a potentially stormy period ahead in German politics.</P>

