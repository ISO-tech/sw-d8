<font face="Times New Roman, Times, serif" size="5"><b>Seeing the cops in action</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 13, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>I am 16 years old. This August, I visited my father in Minneapolis and learned firsthand about police brutality. </P>
<P>I was having dinner with two friends, and we had just paid the check when we noticed a squad car had pulled up. Two white male cops went straight over to a homeless Black man, flipped him against a wall and immediately handcuffed him. </P>
<P>My friends and I were disgusted by this obvious racial profiling. One of my friends expressed his disgust by yelling to the police, "You cops are ruining this neighborhood." It wasn't a threat or profanity--just a simple expression of his thoughts.</P>
<P>We had already started to walk down an alley when we saw a cop car behind us. Having just seen a couple of cops rough up a man, we jokingly said to each other, "Run!" </P>
<P>We then ran several steps, not thinking that the car was for us. But apparently, the cops had found my friend's comment to be worthy enough to send a car to arrest us. </P>
<P>The cop jumped out of his car and threatened to shoot us if we didn't stop; we hit the ground spread eagle. He had his finger on the trigger and aimed at our heads.</P>
<P>As we lay helpless, he threatened to shoot us for causing him to run. More cops arrived, and ordered "him" up (without making a distinction).</P>
<P>I started to rise and was immediately slammed back into the gravel and broken glass. They picked up my friend and slammed him against a wall. He was threatened, insulted and cursed at. Cuffs were put on so tight that his wrists bled. </P>
<P>When I was allowed to go, I went to the squad car to find out where he was to be taken. I was then threatened with arrest "for interfering with an arrest" (for asking questions). </P>
<P>My friend was released in an hour after being questioned, but is now awaiting charges for "disturbing the peace" and possibly "interfering with police business."</P>
<B><P>Mathew Hanson,</B> Washington, D.C.</P>

