<P><font face="Times New Roman, Times, serif" size="5"><b>Florida vote fraud lawsuit settled</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 13, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE FINAL round of the slugfest over the 2000 presidential election in Florida came to an end last week when the NAACP and other civil rights groups settled their federal lawsuit against state and local election agencies.</P>
<P>While no Florida election officials admitted wrongdoing, the settlement requires them to make changes in their election procedures to avoid a replay of the 2000 vote, in which tens of thousands of Black and Latino voters were barred from the polls. Some eligible voters found themselves removed from the voting rolls, while other votes weren't counted because of antiquated equipment and bungled registrations.</P>
<P>No doubt Florida Gov. Jeb Bush was relieved by the settlement. The deal heads off a trial that would have generated weeks of headlines about the racism and dirty tricks used to guarantee a win for his older brother George--just as Jeb is heading into the homestretch of his reelection campaign.</P>
<P>But the first big test of the new system this November might bear a resemblance to 2000. Last week, Carrie Meek, an African American member of the U.S. Congress, stopped by a Miami branch library to cast her absentee ballot in next week's primary vote. She was turned away when a county computer couldn't verify that the 10-year congresswoman is an eligible voter.</P>

