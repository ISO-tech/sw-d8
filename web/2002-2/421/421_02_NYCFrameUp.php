<P><font face="Times New Roman, Times, serif" size="4">Truth finally comes out about Central Park jogger case</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Racist frame-up exposed in NYC</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mike Stark</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 13, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE RACIST hysteria used to convict five Black teenagers of a notorious crime in New York City more than a decade ago has finally been exposed.</P>
<P>DNA testing has proved that another man, Matias Reyes, raped a 28-year-old woman jogging in Central Park in April 1989. Lawyers for three of the five young men, who have finished serving prison sentences, are demanding that the New York Supreme Court overturn the convictions.</P>
<P>The rape and brutal beating of the Central Park jogger caused a media outcry. New York police rounded up a group of 30 young men--and obtained confessions from five, whose ages ranged from 14 to 16: Kevin Richardson, Antron McCray, Raymond Santana, Yusef Salaam and Kharey Wise. </P>
<P>Lawyers for the defendants say that the confessions were coerced and, in fact, contradicted known facts about the case. No witnesses saw the attack take place, and the victim was so badly beaten that she had no memory of the crime. Most importantly, no physical evidence linked the five to the crime--no blood, no fingerprints, no hair or clothing samples, nor DNA evidence of any kind. </P>
<P>But New York prosecutors managed to get convictions anyway--thanks to the lynch-mob atmosphere fed by outrageous media stories and politicians competing for public attention. Local and national newspapers eagerly reported unsubstantiated stories leaked from the New York cops that the youths were laughing, joking and singing "Wild Thing" in reference to the attack.</P>
<P>Meanwhile, incumbent Mayor Ed Koch and challenger David Dinkins were battling for the Democratic nomination for mayor, which Dinkins eventually won. The two dueled to express their outrage--with Dinkins calling the five Black and Latino defendants "urban terrorists" and Koch latching onto the phrase "wilding" to describe the attack.</P>
<P>Billionaire Donald Trump took out a full-page ad in the <I>New York Times</I> calling for the reinstatement of the death penalty--helping to begin the political momentum for the reinstitution of capital punishment in New York in 1995.</P>
<P>With the DNA evidence implicating Reyes, prosecutors are looking for cover. Not only do they have to account for the five convictions, but Reyes raped several other victims after the Central Park attack.</P>
<P>No wonder they are sticking to their story that Reyes was a sixth attacker who was never apprehended. But this claim contradicts Reyes's established profile of acting alone. And efforts to link Reyes to the five through photo identifications or other methods have turned up nothing.</P>
<P>It's time for New York City officials to admit the terrible injustice they committed when they whipped up a racist hysteria to railroad five young men to prison.</P>

