<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISRAEL/PALESTINE</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Israeli military escalates attacks</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Green light for a new rampage</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 13, 2002 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ISRAELI FORCES got a green light last week for a renewed rampage against Palestinians throughout the Occupied Territories.</P>
<P>Israeli officials were warning of a Palestinian "mega-attack" on the eve of the Jewish New Year, claiming that they intercepted a giant car bomb in Palestinian territory that was bound for Israel.</P>
<P>As the Palestinian Legislative Council met in the West Bank for the first time in a year, 60 Israeli tanks staged a fresh incursion into Gaza. A few days earlier, in the West Bank town of Tubas, Israeli forces used a missile to assassinate a Palestinian activist--but missed their target and instead killed two teenagers and two children.</P>
<P>And in Khan Younis in Gaza, a complex of metal workshops was razed to the ground by a helicopter missile strike. Israeli officials claimed that the factories were used to make ammunition--but didn't bother offering any evidence. "Why did they hit my foundry?" asked Abu Khalil, explaining that he built parts for electric generators. "I did nothing wrong." </P>
<P>But "doing nothing wrong" doesn't matter to Israel. The country's High Court made that clear last week when it ruled that the Israeli military could forcibly transfer people from the West Bank to Gaza, though only if they were personally involved in "serious crimes."</P>
<P>The military immediately took advantage of the ruling, transferring two siblings of an accused Palestinian militant who was assassinated August 6. None of the three ever stood trial or had any evidence presented against them. In a statement, United Nations Secretary General Kofi Annan said that the transfer policy "amount[s] to collective punishments."</P>
<P>But collective punishment against the entire Palestinian population has been taking place for months--with a large proportion of the 3 million Palestinians in the Occupied Territories under military curfew at any given time.</P>
<P>In the West Bank city of Nablus, conditions are especially dire. "The situation is very critical in Nablus," said Dr. Mustafa Barghouti, president of the Union of Palestinian Medical Relief Committees. "People have been living under constant curfew for almost three months and are now being denied access to medical treatment."</P>
<P>While the drive to crush the Palestinian uprising continues, Sharon and the rest of the Israeli establishment are chomping at the bit for a U.S. war on Iraq. Israeli officials have already leaked their plan to use nuclear weapons if Iraq attacks Israel--claiming that such a strike "could destroy Iraq as a state."</P>
<P>And they have already authorized the U.S. to stockpile military hardware at Israel's army bases and pledged to provide the U.S. with logistical and intelligence aid. "The quantities of arms which have already been shipped to Israel are enormous because Israel is the only country in which we have such confidence," explained one U.S. officer.</P>
<P>Several Israeli commentators say that a war with Iraq could provide the opportunity to carry out a mass expulsion of Palestinians from the West Bank and Gaza.</P>
<P>Gamla--a far-right group founded by Israeli settlers and retired military officers--posted a particularly graphic sketch on the Internet July 3. "Israel issues a warning that, in a response to any terrorist attack, she will immediately completely level an Arab village or settlement, randomly chosen by a computer from a published list," the group declared. </P>
<P>"The Arabs residing there will be evicted without compensation, all houses and buildings completely demolished, and the settlement itself, with the help of bulldozers and any other necessary equipment, will be leveled into a large field. After the appearance of several such fields, the Arabs will lose any desire to commit terrorist attacks, and the number of Arabs wanting to leave [greater Israel] will certainly increase."</P>
<P>This is ethnic cleansing, pure and simple. "A few years ago, it would have been easy to dismiss the Gamla document as the work of marginal extremists," wrote Ali Abunimah, a contributor to the Electronic Intifada Web site. "But in today's Israel, where pro-ethnic cleansing ministers sit in the cabinet, and even those who would not support transfer are opposed to coexistence and equality, it is a worrying sign."</P>

