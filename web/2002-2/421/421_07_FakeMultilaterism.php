<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Backroom deals to set up a war on Iraq</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 13, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AS BUSH administration officials took to the airwaves on the Sunday morning talk shows last weekend to make the case for war on Iraq, there was a seeming role reversal.</P>
<P>Vice President Dick Cheney--previously adamant on the U.S. government's right to "go it alone" in a war against Iraq--said on <I>Meet the Press</I> that the U.S. is "trying very hard not to be unilateralist."</P>
<P>Meanwhile, on <I>Fox News Sunday,</I> Secretary of State Colin Powell--who insisted in recent weeks on the need for United Nations (UN) backing for war--was sending a different message. "The president will retain all of his authority and options to act in a way that may be appropriate for us to act unilaterally to defend ourselves," Powell said.</P>
<P>These statements are evidence that members of the Bush administration are not nearly as divided as they have appeared to be over war in Iraq. The entire administration has been unified on the U.S.'s key war aims since the start of the "war on terrorism."</P>
<P>Bush used the September 11 attacks as an excuse to launch an open-ended war aimed at furthering the ability of the U.S., as the world's sole superpower, to intervene militarily anywhere in the world to defend its interests. There are two components to this overarching goal--first, establishing the U.S.'s right to "go it alone" against its enemies; second, its right to launch "pre-emptive" wars for "regime change."</P>
<P>The debate between the unilateralists and multilateralists in the Bush administration developed in recent weeks as it became increasingly clear that the U.S. would need to sacrifice its first goal in order to achieve the second in the case of Iraq.</P>
<P>Over the summer, as the international outcry against war on Iraq grew, domestic support for the war also plummeted. An <I>ABC News</I> poll on August 29 showed just 56 percent of people in favor--down from 78 percent last November--of U.S. military action against Iraq to force Saddam Hussein from power.</P>
<P>Support for war on Iraq rises to 65 percent if the U.S. launches a multilateral war (the figure for Europeans rises to 60 percent), according to a survey commissioned by the Chicago Council on Foreign Relations and the German Marshall Fund. </P>
<P>This explains the Bush administration's new willingness to garner international support. But war is no less likely than it was before--and when it starts, it will be no more justified, even with the blessing of the UN.</P>
<P>After all, the UN endorsed the 1991 Gulf War--seven weeks of carpet-bombing that killed between 100,000 and 200,000 Iraqis. The UN has also backed 12 years of murderous sanctions on Iraq that have killed well over 1 million civilians, half of them children under the age of 5.</P>
<P>Today, behind all the rhetoric about Iraq's "weapons of mass destruction" emanating from both sides of the Atlantic, backroom deals are being struck to satisfy the real interests of the global powers.</P>
<P>When Bush and Blair met at Camp David last weekend, the focus was on "how to craft a Security Council resolution that will allow the Chinese to abstain and that will secure acquiescence, if not the backing, of the Russians and the French," according to <I>Financial Times</I> columnist Philip Stephens.</P>
<P>Meanwhile, "U.S. diplomats--diligently laying down groundwork for war--are brandishing carrots and sticks at numerous countries," wrote left-wing syndicated columnist Norman Solomon.</P>
<P>The U.S. is sweetening the pot with assurances to Russia and France that their business interests in Iraq--including lucrative contracts now frozen because of UN sanctions--will be strengthened under a post-Saddam government. As Dmitry Trenin, of the Carnegie Moscow Center, predicted, Russia "will not cast a veto [at the UN] because&#133;that would mean restarting a confrontation with the U.S."</P>
<I><P>Newsweek </I>columnist Fareed Zakaria laid out the case for UN backing as nothing more than an exercise in public relations. "Even if the inspections do not produce the perfect crisis," Zakaria wrote, "Washington will still be better off for having tried because it would be seen to have made every effort to avoid war."</P>

