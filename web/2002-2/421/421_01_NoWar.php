<P><font face="Times New Roman, Times, serif" size="4">UN support won't justify war on Iraq</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Stand up against Bush's war!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 13, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH is looking for war--and he's getting his posse in order. Cynically using the anniversary of last year's September 11 attacks to push for more horror in Iraq, Bush was due to address the United Nations (UN) on September 12.</P>
<P>After weeks of debate in Washington--not over whether to go to war, but how--the Bush gang has cranked up its propaganda machine. And if there's no evidence, some can always be made up.</P>
<P>Like last weekend, when administration officials took over the Sunday talk shows to claim that Iraq possesses nuclear weapons. They cited a satellite photo supposedly showing new construction at several sites linked in the past to Iraq's weapons programs. The White House later admitted that the photo could mean just about anything.</P>
<P>Bush hacks also claimed that a 1998 International Atomic Energy Agency report concluded Iraq was six months away from developing nuclear weapons. Turns out the report said the exact opposite--that there was zero chance of this happening.</P>
<P>Washington's lies may be shamelessly transparent. But don't think that they'll have too much trouble lining up international support. Predictably, Bush's favorite lapdog--British Prime Minister Tony Blair--is already on board. And, as <I>Business Week</I> magazine suggested, other governments will come around--as soon as the administration cuts deals to give Russia, China, France and others a piece of the economic pie in "post-Saddam" Iraq.</P>
<P>That's why Bush will be politely received at the UN. This, after all, is the institution responsible for deadly economic sanctions that have killed more than 1 million people during the last decade--and that willingly lined up behind George Bush Sr.'s first Gulf War in 1991. The UN isn't a force for peace--but a fig leaf for war.</P>
<P>In contrast, support for Bush's war drive has been dropping steadily among ordinary people--around the world, but more recently in the U.S. itself. From Portland, Ore., to Crawford, Texas, to Louisville, Ky., Bush has been dogged by hundreds of protesters wherever he speaks. Tens of thousands of people will attend peace vigils and antiwar events in the run-up to the anniversary of the September 11 attacks--to send a very different message from the flag-waving politicians.</P>
<P>And in the weeks to come, meetings on campuses and in neighborhoods will bring together veteran activists and people new to the antiwar opposition to discuss how we can build a movement that can stop Washington's plan to commit new horrors in Iraq.</P>
<P>We need to stand up now--and say no to Bush's war.</P>

