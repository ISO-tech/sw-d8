<P><font face="Times New Roman, Times, serif" size="4">New bombing on Vieques</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Used by the Navy for target practice</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By H&eacute;ctor Reyes</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 13, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. Navy resumed bombing exercises on the Puerto Rican island of Vieques September 3.</P>
<P>And again, the Navy was met by resistance from a population that is determined to stop the U.S. war machine from using their land for target practice. Twelve people were arrested in the latest round of civil disobedience at the base, and U.S. soldiers responded by firing tear gas at protesters.</P>
<P>The Navy has been bombing Vieques for more than 60 years, taking over more than two-thirds of the island, which lies southeast of Puerto Rico. The rate of cancer in Vieques is 30 percent higher than in the rest of Puerto Rico.</P>
<P>The current phase of resistance began after a Vieques resident--David Sanes--was killed during a bombing exercise in 1999. People from Vieques and around Puerto Rico responded by occupying the shooting range for a year.</P>
<P>In May 2000, the Navy--working with FBI agents, U.S. marshals and soldiers--removed the protesters, arresting hundreds. Since then, the number of people arrested in civil disobedience actions stands at more than 1,300. Protesters have torn down more than 30,000 feet of fencing around the base.</P>
<P>In response to the pressure, the Navy said it would use only inert bombs when it resumed exercises in 2000. But residents complain that inert bombs still produce loud noises that affect their health and spray airborne contaminants--including depleted uranium that the Navy claims it used by mistake.</P>
<P>George W. Bush has promised to move the Navy out of Vieques by May 2003, but Puerto Ricans fear that he won't keep his word. Activists called on Bush to make his pledge official by issuing an executive order guaranteeing the Navy's departure. Meanwhile, Puerto Rico's governor Sila Calder&oacute;n is clearly wavering on her election promise to champion the cause of Vieques.</P>
<P>Only actions of ordinary Puerto Ricans and their supporters will make sure that the Navy leaves next May.</P>

