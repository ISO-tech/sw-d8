<P><font face="Times New Roman, Times, serif" size="5"><b>They're split on attacking Iraq</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 13, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>There has been a fierce debate in Washington and around the world concerning Bush's wish for war on Iraq. <I>SW</I> has recently characterized this debate as a matter of "not if, but when" (August 9). At this stage, I don't think that this is true. </P>
<P>What they are debating is whether or not to invade Iraq and overthrow Hussein, and what the costs of that--political, economic and military--will be. If the debate was limited to "when, not if," the only difference of opinion would be numbers of U.S. troops, bombs and so on. </P>
<P>The reason they aren't carpet-bombing Iraq right now testifies to the seriousness of the divisions within the Bush administration, within the American ruling class and between the ruling classes of the world.</P>
<P>Saying that war is a matter of "when, not if" means that there is nothing our side can do to stop it. What we need to say, especially to activists is this: right now, the people at the top are divided. We need to take advantage of their infighting before the debate does become a matter of "when, not if." </P>
<B><P>Binh, </B>New York City</P>

