<P><font face="Times New Roman, Times, serif" size="4">Union holds a second vote on job-killing contract offer</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>High stakes for IAM at Boeing</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 13, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MACHINISTS AT Boeing will vote on a proposed contract September 12-13--the same union-busting offer that they voted on just two weeks earlier.</P>
<P>The deal would allow management to increase health insurance premiums for family coverage tenfold--and give the company the right to bring nonunion subcontractors into the commercial aircraft assembly lines.</P>
<P>Already, Boeing has carried through most of the layoffs of 30,000 IAM members that it announced last year. The 26,000 workers, members of the International Association of Machinists (IAM), were casting ballots August 29 when union officials announced that a federal mediator had "ordered" the IAM to negotiate with the company. In fact, the mediator had no such authority to make that order--and Boeing never agreed to extend the offer.</P>
<P>When the mediation effort broke down last week, IAM President Thomas Buffenbarger sent a letter to members attempting to justify the union's decision not to count the first vote. "Had we counted those ballots, Boeing would have had the strike they sought and/or the substandard contract they proffered," he wrote. "Neither was in our members' best interest." In other words, IAM leaders decided members' "best interests" by denying them their vote--a move that angered the rank and file.</P>
<P>And with top IAM officials in Washington, the job of organizing against the contract was left to rank-and-file activists like Don Grinde in District 751 in Everett and Keith Thomas in Local Lodge 834 in Wichita, Kan. </P>
<P>Where Buffenbarger's letter stressed that workers would lose $28 million a week in wages during a strike, Thomas distributed a newsletter arguing that company would still lose $2 billion if the IAM walks out.</P>
<P>"They aren't holding any mass meetings to discuss the contract," Thomas said. "Buffenbarger's call to arms was like a call to surrender. He said we should reject, but didn't give us any reasons. It's the theater of the absurd."</P>
<P>"There has been a lot of fluctuation in the rank and file since the first vote," Thomas said. "The stress level in the plant is absolutely unbelievable. You can't believe the company or the union. Anything is possible."</P>
<P>A strike at Boeing would have huge implications for the IAM and workers everywhere--and would need solidarity from the entire labor movement.</P>

