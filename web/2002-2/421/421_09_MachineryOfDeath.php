<P><font face="Times New Roman, Times, serif" size="5"><b>Resource for halting the machinery of death</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Noreen McNulty</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 13, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>BOOKS: <I>Machinery of Death: The Reality of America's Death Penalty Regime,</B></I> edited by David R. Dow and Mark Dow. Routledge, 2002, 320 pages, $17.95.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>DEFENSE LAWYERS, family members of murder victims, former prison employees, journalists and social workers--all with firsthand experience of the horrors of capital punishment--contributed to the collection of essays titled <I>Machinery of Death</I>.</P>
<P>"The death penalty is a direct descendent of lynching and other forms of racial violence in America," writes Stephen Bright, director of the Southern Center for Human Rights. Bright shows how the system sentences a disproportionate number of Blacks to death--and how the courts have set an unreasonable standard of proof for racial discrimination.</P>
<P>Stories of racism abound--jurors threatened by the Ku Klux Klan, Blacks routinely stricken from juries, and judges and even public defenders using racial slurs in court.</P>
<P>A number of essays address the issue of innocence. As attorneys Mandy Welch and Richard Burr illustrate in their article about Shaka Sankofa (also known as Gary Graham)--executed by then-Texas Gov. George W. Bush in 2000--prisoners can no longer appeal on the issue of probable innocence alone.</P>
<P>They point out that public concern about the fairness of the death penalty has grown, as appeals have become so restricted that the possibility of executing an innocent person has increased.</P>
<P>Unfortunately, no articles written by death row prisoners appear in this collection, but in the section "Lives Intertwined," lawyers and investigators describe prisoners' personal paths to death row--stories of poverty, unspeakable childhood abuse, untreated mental illness and unrecognized mental retardation.</P>
<P>In "Representing Robert Sawyer," Sarah Ottinger tells how her client's severe mental illness and brain damage rendered him unable to control his aggressive behavior. His illness went untreated, and he killed the babysitter he believed abused his girlfriend's children.</P>
<I><P>Machinery of Death</I> provides useful arguments about why the death penalty must be abolished.</P>

