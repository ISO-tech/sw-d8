<P><font face="Times New Roman, Times, serif" size="4">How Ebbers and his banker pals got rich</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Corporate crime family</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 13, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CORPORATE CRIMINALS don't act alone, and WorldCom CEO Bernard Ebbers is no exception. While Ebbers was CEO, WorldCom overstated its profits by $7 billion between 1999 and 2001--the largest accounting fraud in U.S. history. But Ebbers' career in corporate crime began years earlier.</P>
<P>In exchange for steering WorldCom to Salomon Smith Barney for investment banking services, Salomon gave Ebbers several opportunities to buy initial public offerings (IPOs) of technology stocks--a literal gold mine for anyone who got in on the ground floor. For its part, Salomon made a killing as WorldCom's investment banker, raking in $80 million in fees between 1998 and 2001.</P>
<P>Ebbers and Salomon deny that there was any exchange. But the <I>Financial Times</I> wrote in an editorial that Ebbers' IPO deals "[throw] light on widespread back-scratching between high-rolling individuals and the investment banks keen to win business with their companies."</P>
<P>Indeed, Salomon's special treatment for Ebbers' is only one part of the story. Another is Jack Grubman, Salomon's hot shot technology analyst and WorldCom's biggest cheerleader on Wall Street during the 1990s.</P>
<P>Turns out Grubman had a cozy relationship with WorldCom, attending three closed meetings of WorldCom's board of directors. By telling investors to buy WorldCom stock even as the company was tanking, Grubman helped insure that WorldCom didn't take its investment banking business elsewhere.</P>
<P>But Grubman still thinks he's gotten a raw deal. "The relentless series of negative statements about my work, all of which I believe unfairly single me out, has begun to undermine my efforts to analyze telecommunications companies," he whined.</P>

