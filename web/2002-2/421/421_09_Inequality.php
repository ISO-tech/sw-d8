<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Can a revolution end inequality?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 13, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IN ALL past revolutions, the universal goals expressed by their participants masked underlying differences. Appeals to the interests of humanity, "the people" or the nation covered over the fact that there were different class forces with differing ideas about the revolution's goals.</P>
<P>In the French revolution that began in 1789, the slogan "libert&eacute;, egalit&eacute;, fraternit&eacute;" brought together both rich merchants and poor rag pickers, small peasants and shoemakers, against the "old regime" of the monarchy and wealthy landlords.</P>
<P>The outcome of the struggle, though driven from below by the poor of Paris, created a society in which "freedom" became the freedom of the capitalist to exploit the worker, and "democracy" the freedom of the rich to use their wealth to buy political influence and power.</P>
<P>The revolution liberated France from estates, but not from class division. It created a formal equality that disguised real inequality. "The law, in its majestic equality," wrote the French writer Anatole France, "forbids the rich, as well as the poor, to sleep under the bridges, to beg in the streets, and to steal bread."</P>
<P>Marx called this a "bourgeois revolution," in that it established the power of a new class of merchants, bankers and industrialists (the "bourgeoisie") whose economic power had grown up in the womb of feudal society, but who remained deprived of the levers of state power by the feudal-monarchical system.</P>
<P>Socialism can only come about in a way that is the reverse of this process. Socialism cannot grow up as an economic system inside the womb of capitalism. Capitalism only creates the economic conditions--abundant material wealth--that make socialism possible.</P>
<P>A bourgeois revolution is a political revolution that brings to power an already powerful economic class. But the working class--the class of producers upon whose labor capitalist wealth depends--remain economically subordinate so long as capitalism survives. Workers can't gradually increase their economic power in the way that the bourgeoisie did before they seized political power.</P>
<P>Union struggles can alter the degree of exploitation but not abolish it. The working class must first seize political power, and then use that power to make the economic and social transformation of society. But in so doing, they abolish the basis on which classes&#151;and the state upon which they rely&#151;survive.</P>
<P>When the bourgeoisie posed its own rule as the general liberation of humanity, it wasn't true. But the working class, as Marx and Engels argued, is truly the "universal class," whose emancipation would mean the emancipation of all. As Marx wrote: "Does this mean that after the fall of the old society there will be a new class domination, culminating in a new political power?"</P>
<P>And he answered: "The condition for the emancipation of the working class is the abolition of every class&#133;The working class&#133;will substitute for the old civil society an association which will exclude classes and their antagonism, and there will be no more political power properly so called, since political power is precisely the official expression of [class] antagonism in civil society."</P>
<P>"All the preceding classes that got the upper hand," Marx and Engels wrote also in <I>The Communist Manifesto,</I> "sought to fortify their already acquired status by subjecting society at large to their conditions of appropriation. The proletarians cannot become masters of the productive forces of society, except by abolishing their own previous mode of appropriation, and thereby also every other previous mode of appropriation&#133;</P>
<P>"All previous historical movements were movements of minorities, or in the interest of minorities. The proletarian movement is the self-conscious, independent movement of the immense majority, in the interest of the immense majority. The proletariat, the lowest stratum of our present society, cannot stir, cannot raise itself up, without the whole super incumbent strata of official society being sprung into the air."</P>

