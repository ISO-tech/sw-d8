<P><font face="Times New Roman, Times, serif" size="5"><b>Protesting the war at home and abroad</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 13, 2002 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"OUR GRIEF is not a cry for war." That's the message that thousands of people across the country sent as they turned out to mark the anniversary of September 11 by opposing the Bush administration's war at home and abroad.</P>
<P>On September 7, 4,000 people gathered in San Francisco's Golden Gate Park for a "Power to the Peaceful" concert and rally. Sponsored by Global Exchange, Not In Our Name, and September 11 Families for Peaceful Tomorrows, activists spoke of the need to organize to defend civil liberties and oppose war in Iraq and elsewhere.</P>
<P>In New York City, more than 100 antiwar protesters marched on September 6 to confront the special memorial session of Congress. Marchers gathered outside Federal Hall in lower Manhattan with banners reading "No Blood for Oil" and "U.S. out of the Middle East." Speakers denounced plans for war on Iraq as well as the budget cuts passed to increase military spending. </P>
<P>The next day, more than 100 gathered in Brooklyn to protest the detention of immigrants since September 11. Organized by Justice for Detainees, the march had a large organized labor presence.</P>
<P>One family spoke of how their father had been detained and deported to Pakistan--after having been in the U.S. for over 18 years--and said that they've been forced to sell their house and business to follow him. </P>
<P>In a spirited protest the following day, a crowd of 1,500 marched on Broadway chanting, "War for democracy? It's a lie! They don't care if children die!" The march was followed by a lengthy rally in Union Square with speakers on issues ranging from Iraq to Palestine to Vieques. </P>
<P>Organized by Stand Up New York: 9/11 Remembrance for Global Peace and Justice, the march was one of the largest in New York in months.</P>
<I><P>Candice Amich, Laura Durkay, Adrienne Johnstone and Amanda Maystead contributed to this report.</P>
