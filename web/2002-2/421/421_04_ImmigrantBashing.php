<P><font face="Times New Roman, Times, serif" size="5"><b>Anti-immigrant racism on the rise in Europe</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 13, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>For years now, the countries of the European Union have been trying to keep pace with the U.S., first economically, and more recently militarily. Now they're catching up in yet another area--anti-Arab and anti-immigrant racism.</P>
<P>In the past few decades, immigration to Europe has increased, especially from North Africa and the Middle East. But now that Europe's economy is stagnating, pressure is mounting to cut back on the welfare state. </P>
<P>Since no European worker would take this kind of attack lying down, bosses and politicians--both left and right--have found the perfect wedge issue in anti-immigrant bigotry.</P>
<P>A new law in Italy grants immigrants legal residency for only two years, and only if they can prove--from outside the country--that they already have a job waiting. Denmark and Austria have passed laws requiring applicants for citizenship to learn Danish and German. Britain, France and Spain have all recently passed new measures against immigrants, including restricting immigrant workers' ability to bring family members with them.</P>
<P>In this climate, the fascists have gained new confidence. At an international conference of far-right parties in late July, Austrian bigot J&ouml;rg Haider noted that both conservative and social-democratic parties were echoing his rhetoric.</P>
<P>Though the fightback remains weak for now, anti-immigrant racism is a key political question facing European workers. Just as in the U.S., fighting back against the bosses' attacks means confronting racism and organizing a movement that unites workers of all nationalities.</P>
<B><P>Brian Chidester, </B>Salamanca, Spain</P>

