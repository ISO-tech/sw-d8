<P><font face="Times New Roman, Times, serif" size="5"><b>West Coast dockworkers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sue Sandlin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 13, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AS CONTRACT negotiations resumed, members of the International Longshore and Warehouse Union (ILWU) at the Matson terminal in Oakland, Calif., showed how best to deal with the shipping bosses and the Pacific Maritime Association (PMA).</P>
<P>When workers discovered that non-ILWU members were doing ILWU work, they held a work stoppage--and won their demands in a matter of 20 minutes. Similar brief actions have happened at other places along the coast.</P>
<P>These kinds of actions point to the way longshore workers can fight back and win. The ILWU leadership has hushed up news of the stoppages for fear that these kinds of actions might spread.</P>
<P>Until last week, the ILWU had been extending the contract on a 24-hour basis. Though negotiations have been resumed, the contract has not been extended. This means that longshore workers are working without a contract, which gives the union the legal right to take job actions.</P>
<P>The PMA has threatened to lock out the union over such actions. And the Bush administration has threatened to intervene by banning a work stoppage under the Taft-Hartley Act and even using military strikebreakers.</P>
<P>ILWU President James Spinosa got Democratic politicians to call on Bush to stay out of negotiations--but then justified the ILWU's demands in the name of "national security."</P>
<P>But the ILWU has already agreed to surrender more than 1,200 clerks' jobs. And no amount of letter-writing, boycotting or speechifying politicians will make a bit of difference to employers who are out to defend their profits at all costs.</P>
<P>The only way to effectively fight the PMA is for the ILWU to use the only power it has--the power to shut down the ports. The successful work stoppage at Matson last week shows how it can be done.</P>

