<P><font face="Arial, Helvetica, sans-serif" size="2"><b>A PEOPLE'S VACATION GUIDE</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The heart of the fight for civil rights</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Petrino DiLeo</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 13, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SITUATED IN the Alabama city that was a center of the civil rights movement of the 1950s and '60s, the Birmingham Civil Rights Institute does an impressive job of chronicling the key struggles. The museum describes the struggles to challenge racism in the wake of Rosa Parks' historic refusal to move to the back of the bus in nearby Montgomery.</P>
<P>But the struggle didn't come without massive repression. A display of an 18-foot cross like the ones racists burned in front of African American homes stands as a reminder of the Ku Klux Klan's presence. One area of the city became known was "Dynamite Hill" because so many Black activists' homes were destroyed there.</P>
<P>Birmingham's movement reached its apex in 1963 with sit-ins, marches and confrontations with the city's segregationist police chief Eugene "Bull" Connor. Connor called in fire hoses and police dogs to attack protesters, led by Martin Luther King Jr., who marched in defiance of a court injunction. King was arrested. In jail, he wrote his famous "Letter from the Birmingham Jail," which is replicated at the museum.</P>
<P>The most moving part of the museum is devoted to the September 1963 bombing of the 16th Street Baptist Church, in which four young Black girls were killed. After reading about the girls and the events leading up to the bombing, visitors are led into a room that's empty except for a giant window that perfectly frames the reconstructed 16th Street Church, which sits across the street.</P>
<P>The church's basement has been restored and turned into a small museum. A burnt-out bus serves as the centerpiece of another exhibit on the Freedom Riders, detailing the repression faced by Black and white students fighting to desegregate interstate bus lines in the South.</P>
<P>The museum underscores the central role of ordinary people with exhibits of life-sized statues of groups of people, symbolizing the mass marches that were critical to the movement's success.</P>
<P>And then there's the city of Birmingham itself. Once the main source of the South's iron, steel and coal--so much so that a giant statue of the Greek god Vulcan sits on a hilltop overlooking the city--Birmingham was abandoned by the industries beginning in the 1950s.</P>
<P>Unions, crippled by the bosses' decades-long success in pitting white workers against Black, were powerless to stop the exodus of jobs. Abandoned plants dot the city.</P>
<P>The conditions in Birmingham are a powerful argument for a new civil rights movement to bring social and economic equality.</P>
