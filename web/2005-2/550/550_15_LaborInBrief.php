<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">July 8, 2005 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Wisconsin state workers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Robson, AFSCME Local 171</b></font><p>

<font face="Times New Roman, Times, serif" size="3">MADISON, Wis.-- About 100 Wisconsin state employees demonstrated here June 30 on the second anniversary of the expiration of their old contract. <p>
After gathering at the Office of State Employment Relations, members of AFSCME and the AFT marched behind a New Orleans-style brass brand and a coffin for the "deceased good-faith bargaining" for state employees. After a brief "funeral ceremony" on the stairs of the state capitol, the demonstration then marched down to the University of Wisconsin-Madison campus where they ended with a funeral cake and beverages.<p>
The demonstration was the latest in a series of demonstrations organized by the State Employees Action Coalition (SEAC). With Democratic Gov. Jim Doyle demanding major concessions from all of the state employee unions and very little being done to fight it, SEAC was initiated by a number of mainly Madison area AFSCME and AFT locals to try to organize rank-and-file state employees to fight back against the concessions.<p>
Seven state employee unions recently agreed to concessionary contracts retroactive to 2003-2005, and most AFSCME locals have only just reached tentative deals for contracts covering the same period. However, official union activism has totally stopped, even though eight unions still do not have 2003-05 contracts.<p>
SEAC is organizing for a "no" vote on the latest tentative agreements and supporting activists in unions that have already settled and are now trying to get a better contract this year. SEAC argues that workers for the state shouldn't be the ones to have to pay for the state budget deficit caused by large tax cuts for state corporations and the rich.<p>
There's no reason for state employees to take large increases in health insurance premiums and other concessions while settling for next-to-nothing in raises. With many AFSCME members earning less than $10 per hour, our unions should demand raises that can improve workers' standard of living. Instead, union officials and Gov. Doyle are arguing over how much workers' paychecks should be cut.<p>
AFSCME members should vote down this rotten contract--and get ready to join with the rest of the state employees in fighting for a better deal for everyone.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Aurora Health Care</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Robin Gee</b></font><p>

<font face="Times New Roman, Times, serif" size="3">MILWAUKEE--"If you want better pensions, you should just marry well." That's what members of the Wisconsin Federation of Nurses and Health Professionals (WFNHP) Local 5012 say the vice president of human resources for their employer, Milwaukee-based Aurora Health Care, told their bargaining committee recently.<p>
The company is proposing to cut pension and insurance benefits as well as make changes to overtime eligibility rules that would effectively eliminate overtime benefits to part-time staff.<p>
Frustrated by their boss' sexist remark and by the company's unwillingness to negotiate fairly, the nurses took action June 24. Two busloads of nurses wearing wedding gowns and veils and singing their own version of "Going to the Chapel" showed up at Aurora CEO Ed Howe's residence to ask him to marry them. <p>
Local 5012 President Pam Mueller, in an article posted on the union's Web site, said Howe "more than qualifies as the groom." Howe enjoys a $3 million-plus annual compensation package "while he shamelessly asks the little people to take cuts." In 2003, Howe made $3.2 million including $1.3 million that was contributed to a supplemental retirement plan and deferred compensation, according to a recent IRS report.<p>
WFNHP, an AFT health care local, represents about 150 nurses and technicians. It's the only unionized workers at Aurora Burlington Memorial Hospital, a health care facility in Milwaukee that employs 24,000.<p>

