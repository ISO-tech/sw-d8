<font face="Arial, Helvetica, sans-serif" size="2"><b>REVIEWS</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Mixing politics and profit, the DeLay way</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Cindy Beringer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 8, 2005 | Page 13</font><p>

<font face="Times New Roman, Times, serif" size="3"><font face="Arial, Helvetica, sans-serif" size="2">Lou Dubose and Jan Reid,<b><i> The Hammer: Tom DeLay: God, Money, and the Rise of the Republican Congress</b></i>. Public Affairs Books, 2004, 306 pages, $26.</font><p>

<font face="Times New Roman, Times, serif" size="3">YOU CAN read about the rise and, hopefully, the fall of an unremarkable former roach killer from a Houston suburb in <i>The Hammer, </i>titled after the nickname Tom DeLay earned for his strength as an enforcer.<p>
In 1984, DeLay was elected to the U.S. House of Representatives. Here, he found Jesus at "the precise moment in American political history when Jesus became a political asset." This "find" left him without a whit of Christian charity, but his peculiar biblical spin on every aspect of human existence has made him the champion of evangelicals.<p>
DeLay became House majority leader in 2003. His extraordinary fundraising ability gave rise to a complex web of PACs and shady associations now known as DeLay, Inc. What was revolutionary about DeLay's tactics, according to the authors, was the scope of his operation. Lobbyists set up shop in DeLay's office and became part of a "kitchen cabinet."<p>
Having lobbyists write bills is nothing new, but DeLay reversed the role of legislator and lobbyist and had "lobbyists do their bidding." "They are asking us to whip bills now," one lobbyist told an interviewer. An energy company in Kansas could be asked to contribute to the election campaigns of congressmen from Texas, Alabama and Louisiana in order to get a favorable provision in an energy bill.<p>
This is how Westar Energy used a conference committee to insert a provision in the 2002 energy bill which would allow Westar to pass on its considerable debt to rate holders. When Westar Vice President Douglas Lake got a request to contribute to DeLay's PAC, he wrote a memo asking, "Who is Shimkus, who is Young? Delay {sic} is from Texas; what is our connection?"<p>
After the system was explained and Westar's check cleared, Westar executives joined DeLay for two days of an energy conference and golf at an exclusive resort.<p>
For real outrage, read the chapter "Saipan," named after a U.S. trust territory in the Pacific Marianas Islands many miles safe from labor regulations. Here, workers--mostly women--from poor areas of Asia pay $7,000 for contracts that bring them to the island to work 70 hours a week, sometimes longer, at $3.05 an hour in "Made-in-the-U.S.A." garment factories. If the contracts are fakes, women are often forced into the island's booming sex trade.<p>
DeLay calls Saipan "a perfect petri dish of capitalism...my Galapagos Island." Working with the island's governor, DeLay and his lobbying associates have fought every effort at reform and turned the Marianas into a "Republican playground." In 1997, House members and their families and aides made 88 weeklong junkets courtesy of the islands' taxpayers.<p>
The book ends with DeLay's "Troubles in Texas," the Travis County district attorney's attempts to bring down DeLay for his part in the Texas redistricting scheme.<p>
Unfortunately, DeLay fails to emerge from this book as a fully developed villain, someone you can really sink your hate into. The authors predict that DeLay will be elected speaker which will make him "in effect...the first prime minister of the United States."<p>
Stay tuned. No matter what form his inevitable fall takes, it won't even come close to the one he deserves.<p>

