<font face="Times New Roman, Times, serif" size="5"><b>Mass protests send a message to the G8</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">July 8, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">MASSES OF people were descending on Scotland in early July to protest George W. Bush and the other leaders of the world's most powerful governments.<p>
The summit of the Group of Eight (G8) rich countries, hosted this time by British Prime Minister Tony Blair, was set to take up a "debt relief" plan that Blair and others claimed would help poor countries in Africa. In reality, the scheme comes with so many onerous conditions that the "G8's plan for saving Africa is little better than an extortion racket," according to journalist and global justice activist George Monbiot.<p>
Some in the global justice movement, including South African activists Dennis Brutus, Patrick Bond and Virginia Setshedi, have criticized the mainstream Make Poverty History campaign for failing to take on the G8 and dropping the demand to completely abolish Third World debt. Bob Geldof, organizer of the international Live8 concert, even pressured performers not to criticize U.S. foreign policy.<p>
That's why the left wing of the global justice movement sought to broaden the debate with a series of teach-ins and protests, such as the G8 Alternative Summit on July 3.<p>
Here, JO HARVIE of the Scottish Socialist Party (SSP) describes the July 4 Make Poverty History march in Edinburgh, leading up to the July 6 rally just outside the G8 meeting in Gleneagles.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">OFFICIAL ESTIMATES put the turnout at over 250,000, but how anyone could possibly quantify the sea of humanity that washed into Edinburgh for the Make Poverty History demo is unimaginable.<p>
Legions of coaches lined every pavement edge, pouring their passengers out onto the green of the Meadows. Every train pulling into Waverly station sent another wave of hundreds of people surging across the Bridges, south to the assembly point of the biggest demonstration Scotland has seen in living memory.<p>
Peace campaigners, debt campaigners, church congregations and the organized left. Environmentalists, fair trade activists, trade unionists and antiwar campaigners. Grannies, mums, dads and weans. All were united around one common aim--sending an unequivocal message to the eight men that will gather to meet in the opulence of Gleneagles hotel on July 6, that enough is enough.<p>
The numbers assembled were so huge that people queued for hours to be part of the enormous demonstration winding a circle round the Edinburgh city center. From morning until the sun that had blazed all day was beginning to fade, thousands waited patiently to parade their message through the streets.<p>
Masked riot police gave no explanation for their decision to pen in one group of demonstrators on one side street. When perplexed marchers asked why they were refused access to one of the main routes back to the train station, the scene suddenly turned frightful as police raised their shields without warning and charged, scattering protesters, including families with children.<p>
"The reaction of the police was absolutely over the top," said Frances Curran, an SSP member of Scotland's Parliament (MSP), who tried to intervene. "The policeman I spoke to had his face covered, I could only see his eyes. These weren't ordinary police, they were specialized riot police." The penned-in protesters were later released one by one, with no arrests made.<p>
Curran and three other MSPs from the SSP were suspended without pay or allowances for a June 30 protest in the Scottish Parliament demanding the right to protest legally at Gleneagles. Even though the Scottish Parliament in March pledged itself to guarantee the freedom to assemble and protest, it has done nothing to ensure the right will be upheld.<p>
The demonstration to come on July 6 is already historic, because marchers have won an agreement from officials to march within earshot of the G8 leaders. Organizers expect somewhere between 20,000 and 50,000 to make their way to the remote luxury hotel and country park, but no one is sure what to expect beyond the fact that the demonstration will show that we will not accept their spin and their lies.<p>
Time is running out for their twisted system--where they spend $1 trillion on arms every year, then expect credit for dropping a few million worth of Third World debt in return for the right to pillage, privatize and profit from impoverished countries' resources.<p>

