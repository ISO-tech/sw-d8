<font face="Times New Roman, Times, serif" size="5"><b>The meaning of the election in Iran</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">July 8, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THE VICTORY of a right-wing populist in Iran's presidential election highlighted social discontent in the country--and will be used by the U.S. as an excuse to intensify its threats.<p>
The immediate pretext for Washington's harsh response to the Iranian election was a report that the president-elect, Tehran Mayor Mahmoud Ahmadinejad, had participated in the 1979 takeover of the U.S. embassy during the Iranian Revolution, in which U.S. diplomats and CIA agents were taken hostage. Then came accusations by an Austrian investigator that Ahmadinejad had played a role in the assassination of a Kurdish leader in Vienna in 1989.<p>
The charges against Ahmadinejad will dovetail with the U.S. and European campaign to force Iran to drop its plans to expand its nuclear energy program--even though the program is permitted under international treaties. Neoconservatives in the U.S. and Israeli right wingers who have been calling for air strikes to hit Iran's nuclear program will seize on Ahmadinejad's victory to claim vindication for their hawkish views. The truth, though, is that the goal of developing nuclear technology was supported by all the main presidential candidates in the first round of the Iranian elections.<p>
The real issues that the election revolved around were poverty and inequality, which worsened dramatically under outgoing President Mohammed Khatami, a reformer. While Khatami offered a degree of political liberalization from Islamist rule, he pursued pro-business policies that exacerbated social polarization--and then stood by and did nothing when the right wing cracked down on progressive student protests.<p>
With middle-class liberals alienated and workers and the poor embittered, there was a political opening for a presidential candidate with a populist appeal. That approach was taken by Ahmadinejad--a hard-liner who had the support of the conservative clergy and the vigilante Basij militia, the volunteer force established by the former Ayatollah Khomeini to crush dissent.<p>
But reformer Mehdi Karroubi, the former speaker of parliament, also campaigned during the first round of the election on a promise to pay every family $60 a month--a significant sum in a country where the average salary is $200 a month, and one in four people live under the poverty line.<p>
Karroubi claimed that he got more votes than Ahmadinejad in the first round, but that the ballot boxes were stuffed. In the second round, millions who voted for Karroubi backed Ahmadinejad, giving him a crushing victory over former President Akbar Hashemi Rafsanjani.<p>
Rafsanjani, a conservative, courted the middle-class reformist vote, but he was seen by workers and the poor as a symbol of an unpopular establishment.<p>
That's why Ahmadinejad's victory shouldn't be interpreted as a lurch to the right by the majority of Iranians. "The poor class who voted for [Ahmadinejad] will expect him to quickly solve their pressing problems--unemployment, inflation, social security, food, housing and widespred corruption" wrote Paris-based Iranian journalist Safa Haeri.<p>
Plus, there are signs that workers are beginning to take the initiative to demand change. Tehran bus drivers recently withstood repression from government security agents to form the first independent union since the 1979 revolution.<p>
U.S. officials may make noises about crackdowns and civil liberties, but the only thing they're really interested in "liberating" in Iran is oil--just as in Iraq.<p>

