<font face="Times New Roman, Times, serif" size="5"><b>"A life free of the threat of violence"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">July 7, 2005 | <i>SW</i> Online special feature</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>GEORGE GALLOWAY is a member of parliament representing East London and a leader of the antiwar Respect coalition.</b><p>

WE EXTEND our condolences to those who have lost their lives today and our heartfelt sympathy to all those who have been injured by the bombs in London.<p>
No one can condone acts of violence aimed at working people going about their daily lives. They have not been a party to, nor are they responsible for, the decisions of their government. They are entirely innocent and we condemn those who have killed or injured them.<p>
The loss of innocent lives, whether in this country or Iraq, is precisely the result of a world that has become a less safe and peaceful place in recent years. We have worked without rest to remove the causes of such violence from our world. We argued, as did the Security Services in this country, that the attacks on Afghanistan and Iraq would increase the threat of terrorist attack in Britain. Tragically, Londoners have now paid the price of the government ignoring such warnings.<p>
We urge the government to remove people in this country from harm's way, as the Spanish government acted to remove its people from harm, by ending the occupation of Iraq and by turning its full attention to the development of a real solution to the wider conflicts in the Middle East.<p>
Only then will the innocents here and abroad be able to enjoy a life free of the threat of needless violence.<p>

