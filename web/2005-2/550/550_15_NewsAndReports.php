<font face="Times New Roman, Times, serif" size="4"><b>Abolish the federal death penalty</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Cynthia Little</b></font><p>
<font face="Times New Roman, Times, serif" size="3">BURLINGTON, Vt.--About 100 people came to a June 22 panel organized by the Vermonters Against the Death Penalty in opposition to one of 12 cases in which the Bush administration is attempting to use federal death penalty charges to reintroduce capital punishment to a state that abolished it. <p>
The panel featured Sandy Baird, mother of a murder victim and former state legislator; David Kaczynski of New Yorkers Against the Death Penalty and brother of the Ted Kaczynski who's widely known as the "Unabomber"; Alice Kim from the Campaign to End the Death Penalty; and Billy Moore, a former Georgia death row prisoner. <p>
Former Attorney General John Ashcroft and current Attorney General Alberto Gonzalez are seeking to try Donald Fell on capital charges. One week after the panel, another 100 people attended a rally to keep up the pressure while Fell still awaits his sentence.<p>
"The government should never have power of life or death over its citizens," said Baird as she pointed out the racism of the death penalty. "Such power is the product of a totalitarian government." Kaczynski spoke about how the death penalty has nothing to do with justice. He didn't want to turn his brother in because he feared a death sentence. <p>
Kim put the death penalty in the larger context. "The most dangerous man isn't sitting on death row--he's sitting in the White House." "If Fell gets the death penalty," continued Kim, "it will open the door to others getting it as well. We know they will be poor, and we know they will be disproportionately non-white." <p>
Finally, Moore told of how prosecutors spent $1.5 million trying to execute him. "That money could go to schools, the poor, hospitals to keep people off the streets," he explained. <p>
The panel was very well received, with lots of applause and a dynamic discussion afterward. About 15 people signed up to get involved with the coalition and join the fight to abolish legal lynching.<p>

<font face="Times New Roman, Times, serif" size="4"><b>No to war and occupation</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Brian Chidester</b></font> <p>

<font face="Times New Roman, Times, serif" size="3">PROVIDENCE, R.I.--More than 50 people from across the state rallied June 27 in front of the federal building here to protest U.S. torture carried out around the world. <p>
The rally was co-sponsored by Amnesty International and the Community Coalition for Peace, with endorsements from other organizations as well. Protesters got the attention of commuters in Kennedy Plaza with chants of "Abu Ghraib, Guant&aacute;namo, U.S. torture's got to go!" <p>
The event marked the anniversary of the signing of the ratification of the International Convention Against Torture. Many passersby signed a petition from Amnesty International demanding "the U.S. government stop all acts of torture against all detainees, launch an independent investigation into allegations of torture, and cease the indefinite detention of prisoners by either charging them with a recognizable offense or releasing them."<p>
Several speakers drew out the connections between the torture scandals and the Bush administration's agenda, pointing to the connection between brutality and torture in U.S. prisons at home and abroad--with the torturers gaining experience at home first. Other speakers denounced the bipartisan priorities of the U.S. government that funds torture and occupation in Iraq while cutting social services at home.<p>

