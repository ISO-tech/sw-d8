<font face="Arial, Helvetica, sans-serif" size="2">July 8, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Hate them or leave it</b></font><p>

<font face="Times New Roman, Times, serif" size="3">TEXAS GOV. Rick Perry has some advice for military veterans who live in Texas and happen to be gay: Get the hell out.<p>
Perry recently traveled to Calvary Christian Academy, an evangelical school in Fort Worth, Texas, to sign legislation requiring minors to obtain written parental consent before undergoing an abortion. While he was there, Perry also signed a resolution putting an anti-gay-marriage amendment on the statewide ballot in November.<p>
The proposed amendment defines marriage as between a man and woman, and additionally would prohibit the state from recognizing civil unions or other similar arrangements. Since Perry's signature was not required to put the measure on the ballot--it had already been approved by enough state representatives--the governor seems to have been trying to send a message about the depth of his loathing for gay, lesbian, bisexual and transgendered Texans.<p>
When a television reporter asked him how the proposed amendment might affect gay veterans in Texas--one of the groups that turned out to protest Perry outside the school--Perry replied, "Texans made a decision about marriage, and if there's a state that has more lenient views than Texas, then maybe that's a better place for them to live."<p>
Perry wasn't the only bigot in attendance, either. Rev. Dwight McKissick, senior pastor of Cornerstone Baptist Church, drew applause when he told the crowd inside that the "gay lifestyle" is a "breeding ground of disease."<br>
-- <b><i>Dallas Voice</b></i>, June 20, 2005<p>

<font face="Times New Roman, Times, serif" size="4"><b>Can you just repeat after me?</b></font><p>

<font face="Times New Roman, Times, serif" size="3">EVER WONDER how Fox News gets its guests to sound so "on message" on shows like <i>Hannity &amp; Colmes</i>? It may be because they're being coached by the best: Sean Hannity himself.<p>
On the March 31 installment of the shouting-head show, the guests included two of the late Terri Schiavo's former nurses, Trudy Capone and Carla Sauer Iyer, who said that their patient wasn't brain-dead. Between commercials, according to an off-air audiotape obtained by comedian Harry Shearer, Hannity coached the women on exactly how to respond when liberal co-host Alan Colmes cross-examined them.<p>
"Just say, 'I'm here to tell what I saw,'" Hannity can be heard instructing his guests. "No matter what the question, 'I'm here to tell you what I saw. I'm here to tell you what I saw.'" Hannity adds helpfully: "Say, 'I'm not going to be distracted by silliness.' How's that? Does that help you? Look into that camera. Look at me when I'm talking."<p>
On the air, Iyer performs beautifully. "I don't have any opinions or judgments. I was there," she declares. After the segment ends, Hannity gushes to the nurses: "We got the points out. It's hard, this isn't easy. But you did great, both of you. Thank you, guys. Those nurses are powerful, aren't they?"<p>
As Shearer commented on his radio show: "Yeah, especially when they do what you tell 'em to do. Very powerful when they follow instructions from the host!"<br>
<i>-- <b>New York Daily News</i>, </b>April 15, 2005<p>

