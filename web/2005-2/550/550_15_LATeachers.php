<font face="Times New Roman, Times, serif" size="4">New leadership faces renewed attack from LA school district</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>UTLA reformers take office</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs, United Teachers Los Angeles</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 8, 2005 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES--Rank-and-file militants of the United Action election slate took office July 1 as the new leadership of United Teachers Los Angeles (UTLA), the second-largest local teachers' local union in the U.S.<p>
Candidates who were part of, or endorsed by, United Action won four out of seven full-time UTLA offices, and 30 out of 49 positions on the union's Board of Directors in elections conducted last February.<p>
The old-guard leadership, headed by ousted President John Perez, did next to nothing during their long "lame duck" period since the elections. Meanwhile, teachers have faced relentless attacks on all fronts--budget cuts from the state government, overcrowded conditions in LA, threats against teacher seniority and pensions by Gov. Arnold Schwarzenegger, and union-busting "restructuring" proposals under Bush's No Child Left Behind (NCLB) law.<p>
With Perez and company finally out of the way, the new leadership takes power with a lot of potential and a lot to prove.<p>
Chasing out a moribund and unpopular union leadership in a relatively democratic union like UTLA is one thing. Fighting the anti-teacher agenda of the massive bureaucracy of the Los Angeles Unified School District (LAUSD) and its Superintendent Roy Romer (a former governor of Colorado and head honcho in the Democratic Party) is another.<p>
But the new leadership and the rank-and-file activists of Progressive Educators for Action (PEAC) have touched a nerve of anger among thousands of LA teachers.<p>
PEAC was the backbone of the United Action slate. Its top candidates--Julie Washington, Joshua Pechtalt and David Goldberg--won election as UTLA vice presidents. United Action also endorsed President A.J. Duffy, who ran as an independent. United Action and Duffy campaigned on a militant platform that promised to mobilize teachers and lead a fight against school overcrowding, NCLB, and attacks on teachers' health care and union rights.<p>
The overwhelming victory of this dissident slate gave a hint of the frustrations felt by UTLA members. Perez and company have done little to resist overcrowding and NCLB and allowed LAUSD to drag out contract negotiation for almost two years.<p>
The result was a lousy deal that the membership voted to accept in April, despite the call for a no vote by the union's incoming leaders--an indication that members' confidence in the union to fight must be rebuilt.<p>
Moreover, the new UTLA leadership will face a new series of attacks. Romer and other district officials plan to restructure 73 schools-- almost exclusively overcrowded schools in low-income neighborhoods--because of their students' low scores on state standardized tests. Parents, students and teachers at these schools do want to see changes, but what NCLB and the district have to offer will only make things worse.<p>
The overwhelmingly Latino and Black student populations at these schools have suffered from systematic inequalities in the educational system, which reach back for decades. But instead of offering more funding or lower class sizes for these schools, LAUSD instead is proposing cosmetic changes to school administrations and squeezing more from teachers.<p>
In response, the new union leaders, in coalition with parent and community groups, are planning a campaign to launch and pass a local ballot initiative for the November election that would implement a special tax on wealthy and commercial property owners to raise $200 million annually for LAUSD schools. They hope this can be a first step toward larger campaigns to fight for even more resources for our schools.<p>
To organize and wield this power, we need to continue to build PEAC into a large citywide network of rank-and-file UTLA activists ready to take up this fight.<p>

