<font face="Times New Roman, Times, serif" size="5"><b>Oil: What the "war on terror" is about</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Dennis Kosuth</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 8, 2005 | Page 13</font><p>

<font face="Times New Roman, Times, serif" size="3"><b><i><font face="Arial, Helvetica, sans-serif" size="2">Oil Factor: Behind the War on Terror,</b></i> a documentary by Gerard Ungerman and Audrey Brohy, Free-Will Productions. Visit <a href="http://www.theoilfactor.com" target="_blank">www.theoilfactor.com</a>.</font><p>

<font face="Times New Roman, Times, serif" size="3">ANTIWAR ACTIVISTS will find the recently released documentary <i>Oil Factor: Behind the War on Terror</i> a useful tool in building the confidence of our side.<p>
This is the most recent creation of Gerard Ungerman and Audrey Brohy, who also brought us <i>Hidden Wars of Desert Storm</i> and <i>Plan Columbia. </i>Their analysis begins with the fact that any government that wants to remain at the top of the food chain needs to control oil supply--not just for their direct use but to have a significant influence on other countries.<p>
The expanding economy of China has produced an equally expanding oil appetite, and when 50 percent of their oil imports originate from the Middle East, this emerging U.S. competitor has an obvious Achilles heel.<p>
The filmmakers explain how the September 11 attacks were used as a pretense to invade Iraq, sold to us with lies about weapons of mass destruction and links to terrorism.<p>
The filmmakers interview Gary Schmitt, executive director for the Project for a New American Century (PNAC)--an organization whose members include Paul Wolfowitz, Dick Cheney and Jeb Bush and was instrumental in selling the war on the Iraq. He comments, "Oil isn't a factor, but having said that, I think it is a good thing that Iraq has oil, and with a dependable friendly Iraqi government, we will be less dependent on Saudi Arabia or Iran."<p>
In another gem, Schmitt says, "I think people would be quite foolish if in fact they thought that money spent on Iraq would be better spent on Medicare or schools here. Our schools get plenty of money. Our need, our priority, must be fixing Iraq."<p>
In grim juxtaposition, an array of images of bombed-out structures is shown, as the voice of a Bechtel representative drones in the background, describing what his company is responsible for rebuilding.<p>
The message is clear: Destroy Iraq and build it up again--all to the benefit of U.S. corporations and funded by taxpayer money. And if oil ever gets flowing, those costs can someday be transferred to the Iraqis. The contradiction between Iraq's natural wealth and the living standards of its people is exposed in shots of blocks-long lines--all waiting to fuel their cars or to get gas for their stoves.<p>
Resistance to the occupation of Iraq is portrayed as legitimate. To return to Schmitt from PNAC, "If in fact things don't go well in Iraq and we have to stay there for a very long time fighting Iraqis, then in fact they will think of us as occupiers."<p>
This quote is interesting considering Defense Secretary Donald Rumsfeld's recent projection of an occupation that will last a dozen years. The footage of an Iraqi man on the street responds in turn, "If anybody comes to Iraq to occupy it, I will fight them."<p>
Narrator Ed Asner reads, "For GIs who were told they'd be going as liberators, the real Iraq war has just begun." Afghanistan, which phased back into the news recently because of the downed Chinook helicopter, is also importantly covered.<p>
Cutting through the lie at the heart of the "war on terror," the documentary shows not only how the U.S. was responsible for the rise of the Taliban and al-Qaeda but had an economic interest in the invasion of that country.<p>
At a recent showing of the film in Chicago, Ungerman said that in general the reception to the film in smaller towns is proportionally better than in the larger cities. This gives lie to the popular idea of a large Middle America where the only option is between pro-war nationalism and ignorant apathy.<p>
The fact that people across the country are organizing showings of this excellent documentary in their homes, schools and community centers is heartening and should be replicated.<p>

