<font face="Times New Roman, Times, serif" size="5"><b>Where are Mexico's Zapatistas headed?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 8, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">IT WAS with its characteristic flair for surprise that the Zapatista National Liberation Army (EZLN) declared a "red alert" on June 20. <p>
In a communiqu&eacute; issued by Internet that shot around the world, the EZLN told its supporters that it was taking this precaution in preparation for a meeting of its supporters in Chiapas to consider "a new step in the struggle, a step which entails, among other things, risking the much or little which has been achieved and worsening the persecution and harassment of the Zapatista communities."<p>
At the same time, the Zapatistas released a letter in which they blasted all of the political parties of the Mexican establishment, including the party of the presumed favorite in the 2006 presidential race, Andr&eacute;s Manuel L&oacute;pez Obrador--the populist Party of the Democratic Revolution (PRD).<p>
It's unlikely that the red alert was a response to a government attack that was about to be launched.<p>
Of course, the possibility of such an attack is ever-present for the Zapatistas, operating small "good government" communities in Chiapas, independent of the state authorities. Likewise, U.S. pressure to step up repression against Central American criminal gangs and drug traffickers--the latest rationalization for the projection of U.S. military force into this resource-rich area--always poses a threat to the EZLN.<p>
Nevertheless, nearly two weeks after the red alert, major troop movements hadn't yet been spotted. As time passed since the "red alert" declaration, it became clearer that the main motivation for the June 20 announcement was political. The Zapatistas have said as much.<p>
The EZLN is undergoing a process of reorganization and discussion that will shortly produce a new political departure for the organization.<p>
Over the course of their existence as an openly proclaimed rebel force, the Zapatistas have taken several initiatives aimed at projecting themselves beyond Chiapas. These have usually coincided with the six-year election cycle of Mexican presidential politics.<p>
Three weeks before the 1994 national election, they hosted the National Democratic Convention (CND) that brought thousands of activists to Chiapas. At the CND, the EZLN attempted to shift Mexican politics to the left by urging a vote against the right-wing National Action and Institutional Revolutionary Parties--a tacit endorsement of populist candidate Cuaht&eacute;moc C&aacute;rdenas of the PRD.<p>
In 2001, the Zapatistas organized a caravan from Chiapas to present to the National Congress a law granting autonomy to the indigenous people of the region. The caravan attempted to test newly elected President Vicente Fox's promise to end the stalemate in Chiapas in "15 minutes."<p>
In both of these initiatives, EZLN hopes were dashed. The governing PRI won the 1994 elections. And in 2001, all the major parties joined together in the National Congress to vote down the Zapatista-backed proposals for autonomy.<p>
So the question today is whether the EZLN red alert is a prelude to a similar proposal or a launching pad to something different.<p>
One well-placed observer, Ana Esther Cece&ntilde;a, a professor at the National Autonomous University in Mexico and a specialist on the EZLN, thinks the Zapatistas might be preparing to launch themselves as a legal political force. "[The red alert] could be a step into the underground, to strengthen the armed organization," Cece&ntilde;a told the Argentinian newspaper <i>Clar&iacute;n</i>. "Or it could be a step to turn itself into a legal political organization. The latter is more likely, without discarding the possibility that the Zapatista army will be left on the one hand, with a political arm on the other.<p>
In their Sixth Declaration from the Lacand&oacute;n Forest, issued June 26, the Zapatistas wrote: "A new step forward in the indigenous struggle is only possible if the indigenous join together with workers, campesinos, students, teachers, employees--the workers of the city and the countryside."<p>
For a movement that had remained largely isolated in Chiapas and refused to project itself as a leadership for working-class people throughout Mexico, this could be a significant shift. But whether it means the Zapatistas see that their fate is tied to that of the Mexican working class or whether they are simply adjusting their populist rhetoric for the upcoming elections remains to be seen.<p>


