<font face="Times New Roman, Times, serif" size="5"><b>Super-rich on a roll</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 14, 2005 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">THE SUPER-rich are grabbing a growing proportion of national income even as the share going to the rest of us declines.<p>
The <i>New York Times' </i>David Cay Johnston reported October 5 that the wealthiest 0.1 percent of the U.S. population--just 129,000 people-- took home a quarter of the entire increase in national income in 2003. <p>
Their income jumped 9.5 percent--compared to 3.7 percent for the rest of the richest 1 percent of the population. All told, the top tenth of 1 percent got 7.6 percent of all U.S. income. <p>
"The top tenth of 1 percent had more income in 2003 than the poorest third of taxpayers, a group with 330 times the number of people, analysis of [IRS tax] data showed," Johnston reported. "This is a sharp change from 1979, the earliest year in the IRS report, when the total income of the poorest third of Americans exceeded that garnered by the top tenth of 1 percent by 2.5 to 1."<p>
The giant sucking sound pulling income upwards is a reflection of broader trends. The U.S. now ranks behind only Mexico and Russia in social inequality.<p>
It's probable that income inequality in the U.S. has only grown worse since 2003, the latest year for which such data is available. That's because the share of corporate income growth going to profits in the current recovery is at a record high--70 percent--with the share of the increase going to wages accounting for 30 percent. This is a near-total reversal from previous business cycles, the Economic Policy Institute pointed out last spring.<p>
What's more, workers' pay continues to lag behind inflation, leaving wages lower in real terms despite rapid increases in productivity. <p>
This overwhelmingly one-sided economic recovery--unseen since the Second World War--has contributed to four straight years of rising poverty, with 1.1 million falling below the already low official poverty line last year alone.<p>
Some 37 million people in the U.S. are officially categorized as poor--and many of them work. "Today, more than 28 million people, about a quarter of the workforce between the ages of 18 and 64, earn less than $9.04 an hour, which translates into a full-time salary of $18,800 a year--the income that marks the federal poverty line for a family of four," <i>Business Week </i>pointed out last year.<p>
The few low-wage employers that offer health insurance to workers are raising the costs--or eliminating coverage entirely. Today, the working poor account for most of the 45.8 million people in the U.S. without medical coverage--a number that's increased by 4.6 million since 2001.<p>
Meanwhile, the Republican Congress is targeting anti-poverty programs like Medicaid for huge cuts--which will only compound the suffering for survivors of the Katrina hurricane and millions of others.<p>
The Democrats oppose these cuts, but at the same time call for "fiscal responsibility" to reign in budget deficits--implying social spending cuts of their own, rather than cutting war spending or taxing the rich.<p>
The worsening inequality in the U.S. amid an economic recovery shatters the mythology that a rising economic tide lifts all boats--and highlights the need to put independent working-class politics on the agenda.<p>

