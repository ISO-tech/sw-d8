<font face="Times New Roman, Times, serif" size="4">From poverty to prison to Nobel Prize nomination</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The life of Stan Tookie Williams</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Cameron Sturdevant</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 14, 2005 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Stanley Tookie Williams, <b><i>Blue Rage, Black Redemption: A Memoir, </b></i>Damamli Publishing Company, 2004, 371 pages, $24.99.<p></font>

<font face="Times New Roman, Times, serif" size="3">STANLEY TOOKIE Williams, the cofounder of the Crips street gang in Los Angles has written an autobiography where his life shares center stage with an almost unending torrent of institutionalized racism, police violence and broken social service agencies. <p>
As <i>Socialist Worker </i>went to press, the U.S. Supreme Court was preparing to hand down what could be one of the final legal decisions in Williams' fight for justice and a reversal of his death penalty conviction.<p>
Williams was convicted in 1981 of murdering four people during two robberies for which he was sentenced to death.<p>
Although Williams regrets his gang involvement, he has always maintained his innocence of these crimes. He has repeatedly said that he will not apologize for crimes he did not commit, even to save his life. <p>
Activists across the country should read <i>Blue Rage, Black Redemption </i>not only to learn about Williams but to gain firsthand insight about how the conditions that shaped his life; racism, poverty, police harassment. Williams points out these faults in American society not as an excuse for his later actions but rather as a constant challenge.<p>
Could the state of California have done anything different? Could it have spent more money on schools? Could children be raised in Los Angeles without pushing their parents to the very edge of sanity? Could the resources devoted to the Los Angeles police department have been better spent on education and job programs?<p>
It is clear from reading <i>Blue Rage, Black Redemption </i>that the answer was yes, but that isn't what happened.<p>
Stan Williams was born in 1953 in New Orleans Charity Hospital. "Because this was 1950s, pre-civil rights Louisiana, my 17-year-old mother, a 'colored woman,' was deprived of anesthetics as her torso was slit from sternum to pubic bone." And that's just from the first paragraph. <p>
The horrific scenes of the disaster left by Hurricane Katrina in New Orleans--also a result of racism, poverty and neglect--give the reader a stark, modern background for Williams birthplace.<p>
Williams takes the reader on an inside journey framed by his mother's struggle against conditions imposed by Jim Crow and poverty, to the streets of South Central Los Angeles, and from there into the maw of the California criminal justice system eventually leading to San Quentin State Prison.<p>
The story of Stan Williams' transformation from gang leader to an internationally recognized children's advocate, repeatedly nominated for the Nobel Prize in peace and literature for his Herculean effort to reduce gang membership and gang violence through his model agreements (which are fully published in an appendix to the book) show that the criminal justice system in California is truly broken. <p>
Williams' book shows that his successful self-education, deep introspection and clear ability to write about the results of his journey to redemption came about in spite of the California criminal justice system. <p>
It's safe to say that Stan Williams, through his children's books on gang avoidance, <a href="http://www.tookie.com" target="_blank">his Web site</a> with a ready-to-print gang peace accord, the TV movie <i>Redemption,</i> along with countless speeches Williams has made to groups via telephone, has helped curb far more violence than any California elected official. Certainly more than the police.<p>
Williams easily has had a far greater positive impact on the lives of thousands of young people in California and across the country than any prosecutor or judge involved in his case. <p>
Aside from his past work to prevent gang violence, Williams' trial is itself an symptom of how broken the criminal justice system is in California. The case against Williams was built on circumstantial evidence and the testimony of several witnesses who were also facing felony charges. <p>
Stan Williams' honest, revealing story will inspire people fighting for civil rights today. The state of California wants to end the insight and inspiration by executing Williams. Williams' book is a call to action against racism and injustice.<p>
If the call is successful, we can save the life of a man who can do much to revive the fight for civil rights.<p>

