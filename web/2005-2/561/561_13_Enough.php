<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Is there enough to go around?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 14, 2005 | Page 13</font><p>

<font face="Times New Roman, Times, serif" size="3">ECONOMICS, ACCORDING to many definitions, is "the social science that studies the allocation of scarce resources" (Wikepedia). This is the most pernicious of all "common sense" ideas--resources are limited, and, therefore, "difficult" choices must be made about who gets what.<p>
It is used to justify all sorts of inequality and calls for "belt-tightening." The argument that resources are limited is used to pit working-class and poor people against one another, by getting them to think that the gain of other workers is their loss, and vice versa.<p>
Scarce jobs, the argument goes, means some people "take" them from other people. Scarcity underlies the idea that some people are getting government "hand-outs" at the expense of others.<p>
Every government program is presented as a gift that must be paid for by robbing some other program. Peter must be robbed to pay Paul. The wealth and resources of our society are presented as a pie. If you get a slice, someone else doesn't. <p>
The "not enough to go around" undercurrent surrounds discussion around relief efforts for Hurricane Katrina. Republicans in Congress are ready to make "tough choices"--proposing to cut social spending by $35 to 50 billion to "pay for" disaster relief.<p>
One of the proposed cuts is a $574 million cut in food stamps that would throw 300,000 people off the rolls. In short, to provide aid to 300,000 poor people devastated by storms, 300,000 other poor people must be made to suffer.<p>
The truth is that these choices are not imposed by limited resources, but by the economic imperatives, and priorities, of capitalism. As Marx pointed out many years ago, capitalism is the first economic system in which crisis ensues not because of shortages, but because of abundance.<p>
Capitalists suffer when they cannot sell their product, that is, when they have "too much" compared to what they can sell. Moreover, when a particular company "suffers," the pain is not shared. Some don't suffer at all.<p>
Delphi, the auto-parts supplier for General Motors, lost $747 million in the first two quarters and $4.8 billion last year. In order to bring themselves out of bankruptcy, they want their workers to accept cuts in pay and benefits of more than 50 percent.<p>
If the workers are forced to accept this, they will be making (in 2005 dollars) only a few dollars over minimum wage in 1970. But Delphi executives are not making any "tough choices."<br>
The company is offering its executives cash ranging from $50,000 to $475,000, totaling $87.9 million, once the company gets out of bankruptcy or is sold. President Rodney O'Neal would get a total cash bonus of $2.75 million over his $1.15 million salary.<p>
There is a way to eliminate poverty--taxing the rich instead of doling out cash to them, raising wages and cutting executive pay, would be a good start.<p>
Capitalism creates little islands of apparent scarcity that are then used to justify hardship: a local drought ravages a region's food crops, a particular company goes under, a whole country is gripped by economic crisis, and so on.<p>
But taken as a whole, the world economy is resource rich. According to the United Nations Human Development Report, 12 percent of all world military spending would provide primary health care for all, including immunization for all children, eliminate severe malnutrition and halve moderate malnutrition, and provide safe drinking water for all.<p>
In truth, capitalist wealth has been expanding for decades. The pie is in fact getting larger, but the superrich are taking an ever-greater chunk of the wealth they do nothing to produce.<p>
So long as they can convince us that we must accept less to "save" our jobs, or accept cuts in one program to save another, they will continue to get their way.  A key component of rebuilding working-class solidarity is the rejection of the idea that we must compete with each other for scarce resources.<p>

