<font face="Times New Roman, Times, serif" size="4">Bush adviser peddles propaganda in Middle East</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Insulting assumptions</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 14, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">A PATHETIC attempt to rescue the shipwrecked U.S. image among Muslim women was itself moored on the rocks of imperial arrogance. <p>
Karen Hughes, the Bush-appointed spin doctor, recently returned from her Insulting Assumptions tour of the Middle East, where she greeted handpicked audiences of upper-class women with stereotypes and condescension. At two stops, one in Saudi Arabia and one in Turkey, she was taken aback to find that they rejected the feminist packaging she tried to wrap around U.S. foreign policy. <p>
Hughes introduced herself as "a working mom," and proceeded to celebrate what she claimed were the two pillars of her liberated American existence: her ability to drive and to cast a vote. She said she hoped that, one day, Saudi women too could "fully participate in society."<p>
But the 500 Saudi women she addressed did not want to emulate their esteemed guest, and resented the implication that they were being prevented from living full lives. One woman criticized the U.S. as a right-wing country without real freedom of the press, to which Hughes responded, "I sometimes wish that were the case, but it's not." <p>
Then Dr. Siddiqa Kamal, an obstetrician/gynecologist who is in charge of a hospital said, "There is more male chauvinism in my profession in Europe and America than in my country. I don't want to drive a car. I worked hard for my medical degree. Why do I need a driver's license?" Or, more to the point, why would a wealthy Saudi doctor want to drive herself when she can hire a driver? I wonder if Hughes feels oppressed when her driver takes her where she wants to go.<p>
In Turkey, things got even more heated. Hughes spoke to 20 feminist leaders who launched a scathing attack on U.S. policy in Iraq. Hidayet Tuskal, director of the Capital City Women's Platform, said the war in Iraq was taking the lives of women and children every day. As for Hughes' propaganda peddling about bringing democracy or liberating women, Tuskal said, "I'm feeling myself wounded. I'm feeling myself insulted here."<p>
Then Hughes got defensive. "It is impossible to say that the rights of women were better under Saddam Hussein than they are today," she lied. <p>
Actually, it's quite simple to say that it has never been more dangerous to be "a working mother" in Iraq--when more and more women must give up even going outside for fear of getting caught in the crossfire of the U.S. occupation. And what does the right of women to vote amount to when you are under military occupation, and the constitution is manipulated by the occupiers? <p>
The only thing that's impossible is for the PR agents of empire to begin to understand anything about Muslim women. They must all be "helpless victims," "suffocating in hijab" and just waiting to be undressed by the "civilizing mission" of U.S. imperialism, or else the spin would grind to a halt. <p>
If Hughes couldn't bond with women of her own class, then it's no wonder she steered clear of ordinary Muslim women, whose lives have gotten worse throughout the Middle East as a result of U.S. economic and military meddling. The real debate about women's rights in Muslim countries can only take place in the context of a movement to kick the U.S. out.<br>
<b>Manijeh Moradian, </b>New York City<p>

