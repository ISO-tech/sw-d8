<font face="Times New Roman, Times, serif" size="5"><b>Beaten by cops in New Orleans</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 14, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">THREE NEW Orleans police officers have been charged with battery for repeatedly punching a 64-year-old African American man and slamming his head into a wall. A videotape of the assault made by an Associated Press television news team was broadcast around the world.<p>
Police claim that they were trying to arrest Robert Davis for public intoxication. But Davis, a retired schoolteacher, told a reporter that he doesn't drink. "At no time did anybody indicate that I was under arrest," Davis said. "This particular incident, I don't...I don't want to say this, but it was probably racially motivated."<p>
As three officers assaulted Davis, another one turned on the cameraman and his producer, and threatened them for videotaping the incident. "I've been here for six weeks trying to keep my fucking self alive, and you fucking want to come and fuck up my city," an officer shouted in the face of producer Rich Matthews, as he pushed him against a parked car. "Go home!"<p>
Those who saw the videotape were outraged. "They did him like Rodney King," Frank Anderson, a Black construction worker, told a reporter. "Those officers ain't no better than these guys who are stealing things."<p>
Since Hurricane Katrina hit in late August, the New Orleans Police Department has been shaken by several scandals. At least 12 officers are under investigation for looting shops to plunder televisions and jewelry. In one case, police are accused of stealing a hospital generator to keep their beer chilled. In another incident, police allegedly took $4.5 million worth of cars from a local dealership.<p>
The officers, who pled not guilty to beating Davis, have been suspended without pay.<p>

