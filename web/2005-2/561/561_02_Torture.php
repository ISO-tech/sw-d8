<font face="Times New Roman, Times, serif" size="5"><b>Will Bush veto Senate torture ban?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 14, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">DESPITE THE Bush administration's efforts to derail it, the Senate passed legislation last week to prohibit the U.S. military's use of torture against prisoners.<p>
The bill, which bans the use of "cruel, inhuman or degrading treatment or punishment" against anyone in U.S. government custody, passed with 90 senators in favor. But nine senators voted against the legislation, because, they say--along with the Bush administration--prohibiting torture isn't "practical."<p>
Sen. Wayne Allard (R-Colo.) explained to the <i>Washington Post</i> that "all [the new law] does is tie the hands of the Department of Defense at a time when maximum flexibility within the boundaries of the U.S. law is needed." "[W]e are spending far too much of our time and effort on the prisoner abuse issue and not enough time on the quality of our interrogations," Sen. James Inhofe (R-Okla.) complained to the <i>Post</i>, adding, "It is my feeling that the more we air this issue publicly, the more we are emboldening the terrorists." <p>
The Bush administration has threatened to veto the legislation if it passes the House, where it faces a tougher vote. That's because the administration continues to claim that tactics like sleep deprivation, mock executions and "stress-and-duress" interrogation techniques are vital to the "war on terror."<p>
The vote to ban torture came two weeks after the Army began an inquiry into new allegations of prisoner abuse in Iraq in 2003 and 2004, carried out by members of a battalion of the 82nd Airborne Division. <p>
According to a report released last month by Human Rights Watch, three former members of the unit claim that soldiers at Camp Mercury, near Falluja, routinely beat and mistreated Iraqi prisoners--sometimes to extract information and sometimes simply "for amusement." <p>
The three described beatings, exposure to extreme hot and cold, the stacking of prisoners one on top of another, and sleep deprivation. In one incident, a soldier broke a detainee's leg by beating him with a baseball bat. Detainees were also forced to carry five-gallon jugs of water with their arms outstretched until they passed out.<p>
"Everyone in camp knew if you wanted to work out your frustration, you show up at the PUC ["Persons Under Control"] tent," one Army sergeant told Human Rights Watch. "In a way, it was sport." <p>
When the officer tried to raise his concerns about abuse to Army higher-ups, he was told to ignore it and warned that he should "consider your career." "It's still going on now the same way, I'm sure," the sergeant told Human Rights Watch. "Maybe not as blatant, but it's how we do things."<p>

