<font face="Times New Roman, Times, serif" size="5"><b>The threat of the vigilantes</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 14, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">IT WAS so exciting to read in <i>Socialist Worker</i> a few weeks ago that the anti-immigrant vigilante action in Calexico, Calif., had to be cancelled because the racists didn't turn out, and because our side did ("Defeat for the border vigilantes," September 23).<p>
In trying to get folks from Phoenix to go to the counter-rally, I ran into the same argument over and over again--that the Minutemen and the other vigilante groups are just a bunch of nut cases. Better to ignore them than bring media attention to them by building big counter-demos. In fact, one campus activist accused me of exaggerating the danger these armed racists pose.<p>
Yet two recent developments confirm how seriously activists should take these vigilantes.<p>
One is the state of emergency at the border declared by Janet Napolitano, the Democratic governor of Arizona. By outflanking Bush and the Republicans to the right, she is calling for greater police and border patrol presence at the border.  <p>
In fact, when Napolitano was challenged by a right-wing state representative about sending the National Guard to the border--which is what the vigilantes are calling for--Napolitano's spokesman responded not by denouncing the idea, but by saying that this was not a viable option because so many Guardsmen were in Iraq and on the Gulf Coast. In other words, the official political discussion in this state about immigration is now on the vigilantes' terms--now matter how small their "actions" on the border may be.<p>
Even more critical is the violence that the vigilantes' actions lead to. Recently, in Tifton, Ga., a trailer park of mainly Mexican families was attacked by two armed men. Five people were killed--some having been beaten with aluminum bats, others shot.<p>
They may seem like crackpots, but the vigilantes are encouraging racists everywhere in the U.S. to put their words into action--with deadly and terrifying results, like in Georgia.<p>
If the vigilante action last month in California sputtered out, that only means our side needs to redouble our efforts to demoralize the racists for good, and push them back under the rocks they came from--and to keep building that movement to shift the politics of immigration back to our terms: a world without borders!<br>
<b>Jeff Bale, </b>Phoenix<p>

