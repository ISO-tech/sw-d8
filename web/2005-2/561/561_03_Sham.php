<font face="Times New Roman, Times, serif" size="4">Bush administration claims new "milestone" in the occupation</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Sham constitution in Iraq</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 14, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">GEORGE W. BUSH tried to regain some of his plummeting popularity with a speech last week hailing the administration's supposed successes in the "war on terror"--even as a crisis looms over the constitutional referendum in Iraq.<p>
To start with, he turned to an old standby--whipping up fears of terrorism--with the stern claim that the U.S. "and our partners have disrupted at least 10 serious al-Qaeda terrorist plots" since September 11. Then he repeated the fantasy that Iraq is on the road to democracy and sovereignty, with the constitutional referendum set for October 15 the latest milestone.<p>
Just how democratic the "new" Iraq will be was clear from the efforts of officials of the U.S.-backed regime to ram through the proposed draft.<p>
Under the procedures established by the U.S. occupation authority, the constitution will fail if it's rejected by a two-thirds majority in three of Iraq's 18 provinces. But last weekend, the Iraqi parliament quietly changed the rules to be two-thirds of "registered voters"--rather than those actually casting ballots.<p>
The change would have made it virtually impossible to vote the constitution down--even an almost unanimous "no" vote wouldn't be enough if turnout was much below 75 percent. Iraqi officials were forced to reverse the maneuver a few days later, but they clearly hope to use force to intimidate opponents of the constitution.<p>
A nationwide curfew is planned, beginning two days before the vote; Iraq's international borders, airports and ports will be shut down; and travel between provinces will be banned, as well as driving on Election Day.<p>
Likewise, in the name of helping Iraqis get to the polls, U.S. occupation forces conducted major sweeps to detain suspected "terrorists" until after the vote.<p>
Over the last few weeks, the U.S. has carried out a reign of terror in the northwestern province of Al-Anbar. In Operations Iron Fist and Iron Gate, thousands of U.S. soldiers, backed by warplanes, tanks and helicopters, attacked several small cities and towns to "root out" al-Qaeda fighters.<p>
The U.S. military claimed that it killed some 50 "insurgents," but it didn't keep track of the untold suffering of area residents. In his Internet journal, "Iraq Dispatches," journalist Dahr Jamail retold the stories of Iraqis who fled to the village of Aanah, just west of Baghdad.<p>
Mrs. Khamis, a mother of eight, told an Iraqi journalist writing under the name Sabah Ali, "We had to run barefoot; I left the lunch on the stove when the attack began. There was heavy bombing and mortar shelling. We had to run through the side streets with white flags."<p>
In a refugee camp where she ended up, "[t]here is no hot water; I have to give the children cold baths, and the weather is changing...My brother tried to go back to Al-Qa'im three times to get some clothes and stuff from our house but could not go through the check points. We need blankets, food, fuel and medicines...the attack will begin tomorrow."<p>
The assault in western Iraq is an important part of Bush's latest in a long line of lies to rationalize the U.S. occupation--that the country has come under control of foreign "terrorists."<p>
The U.S.'s loyal ally, Britain, has done its part to stoke the fear-mongering, last week accusing Iran of supplying weapons to the Iraqi insurgency. But with its continued savagery in Southern Iraq, where British troops last month bulldozed a Basra prison to release two British spies, the lies are growing thin.<p>
As journalist Patrick Cockburn reported in the aftermath of the Basra outrage, "Ordinary Iraqis were drawing their own conclusion about what had happened in Basra. Abdul Hamid, a goldsmith, said over the phone from the city: 'People here have seen that our government has no authority in Iraq. The British did not respect them when they smashed into the jail, so why should we respect our own leaders?'"<p>
The draft of the constitution that will be voted on this weekend was forced through by leaders of the new Iraqi government, even though it was rejected by representatives of the country's Sunni Muslim minority.<p>
Sunni leaders object to the draft because it would ban former Baath Party members from holding office, and would allow provinces to unite and form regional governments--something that would allow further autonomy for Kurds in the oil-rich North, and add to the power of Shiite leaders who want a similar arrangement in the South.<p>
But the situation can't be reduced to a conflict between all Shia and all Sunnis. According to a September poll by the International Republican Institute, of the more than 80 percent of Iraqis who plan to vote, just 49 percent believe that the charter represents the will of the Iraqi people.<p>
The constitutional sham shows the world what kind of democracy the U.S. is building in Iraq--one based on backroom deals and the terror of U.S. military force.<p>

