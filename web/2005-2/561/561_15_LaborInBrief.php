<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">October 14, 2005 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Santa Cruz, Calif., bus drivers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Callum Rowe and Kenny Swain</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SANTA CRUZ, Calif.--Bus drivers here went on strike October 7 after the Metropolitan Transit District board refused to negotiate in good faith.<p>
A strike by United Transportation Union (UTU) Local 23 had been averted, and a tentative agreement for a nine-month extension was issued. Yet two weeks after the agreement was signed, the Metro board revoked the deal and informed workers that they were to continue driving the buses--with no provision for job security, such as a grievance procedure. <p>
The Metro board claimed that the district doesn't have the funds to meet the drivers' main demands for parity in pay, employee health care contributions and retirement benefits. Yet the board recently found the cash to finance a $1 million multi-year compensation package for Metro's manager.<p>
Bonnie Morr, a bus driver for 17 years and the chairperson for UTU Local 23, is coordinating the pickets with approximately 140 workers at four locations (three are around the clock) and is having regular meetings with the strike captains.<p>
The board accuses the drivers of callous and greedy behavior toward their community. In fact, the striking drivers have been going out in their own vehicles, burning their own gas and shuttling some of the regular riders that they know have no other means of transportation. "We are the face of the Metro--we know [the riders], and they know us," Morr said.<p>
The strike has crippled the county and has received incredible support from the community, including a 100-plus rally of students, faculty and staff that marched from campus to the central bus station to demonstrate solidarity. "This is not about money, this is not about salary. It's about one thing: medical benefits!" one striking bus driver shouted to a crowd of supporters.<p>
The bus mechanics, who are represented by the Service Employees International Union, have a clause in their contract that forbids them from supporting any action by the UTU--although so far they haven't been working. Negotiations are set to continue next week. <p>
The workers have shown their power--and need support in their fight.<p>

