<font face="Times New Roman, Times, serif" size="4"><b>Jonah didn't have to die</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IT'S ALWAYS a tragedy when a small child dies in a house fire. But what's worse is when the child dies in a fire that would have been unthinkable in a rationally run society.<p>
Jonah Flores, a 10-month-old baby in the District of Columbia, died after a fire broke out in his apartment--started by a candle in his room. The candle was there because his family had their electricity disconnected, and he didn't want to sleep in the dark.<p>
His mother, Fritzie Flores, had recently gotten out of the hospital and was unable to pay almost $500 owed to Pepco, the local utility company. Her landlord suggested she apply for assistance from the D.C. Energy Office, which she did. The paperwork granting her assistance was found, approved and ready for processing--the day after the fire.<p>
It's outrageous to think that an immigrant family, mired in poverty, would have to put themselves at such great risk to provide themselves with light and comfort. A system that puts corporate profits before people's needs is one in which babies like Jonah Flores can burn to death because a bill wasn't paid--and one in which hundreds of thousands of people can lose everything, even their lives, because more resources are thrown into an illegal war half a world away than into measures that could lessen the damage of a hurricane. <p>
The more I read about tragedies like the death of Jonah Flores, the more I realize we need to fight for a socialist alternative.<br>
<b>Jeff Skinner,</b> Washington, D.C.<p>
