<p><a name="Beating"></a><font face="Times New Roman, Times, serif" size="4"><b>Beating back the bigots</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SOMETIMES THE stupidity of right-wing bigots is only surpassed by their arrogance. At the antiwar march in Washington, D.C., on September 24, about 30 people from the disgusting group known as the "Protest Warriors" showed up with the intention of intimidating participants in the antiwar movement.  <p>
They set up shop about 20 feet from the Campus Antiwar Network (CAN) table, and started chanting and holding up pro-war signs--some of which had racial slurs against Arabs on them. <p>
They were expecting to be unopposed. They were dead wrong. Myself and a few others alerted members of CAN, and before long about 150 people had gathered round with bullhorns, demanding that they leave our protest. <p>
At first, they only mocked us. But that was before we started pushing. First, we forced them across the street. But that wasn't good enough for us. We chased them down the street, made them turn a corner, and we left them (appropriately) at the Ronald Reagan building, two blocks from the protest. They were clearly demoralized and genuinely shocked that we would put up such a fight.<p>
People are outraged by the war in Iraq, the atrocities in New Orleans and the bullheaded way the right wing is destroying everything in its path. This incident demonstrates that a quickly growing number of us are ready to fight back. <p>
From the Protest Warriors in D.C. to the Minutemen in California, the right wing should know that our side won't stop fighting until every one of them has returned to the sewer they crawled out of!<br>
<b>Alexander Billet</b>, Syracuse, N.Y.<p>

