<p><a name="Sold"></a><font face="Times New Roman, Times, serif" size="4"><b>Sold to the highest bidder</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AMERICANS GENERALLY believe in fairness at the polls. Almost everyone would agree that all registered voters should be allowed to vote, their votes should be counted, and there must be a paper trail for verification. Or how about this: Both sides of a proposition must be allowed to present their case to the voters. <p>
That's pretty much a no-brainer too, but California's Proposition 75 was put on the November ballot simply to silence some of Gov. Arnold Schwarzenegger's harshest critics: our nurses, teachers, police, and firemen, by cutting their unions' political contributions. <p>
When you slash funding from only one side of a debate, the other side has a distinct, unfair advantage. Money buys votes.<p>
I agree that we need to reduce the big-buck contributions that fuel our politicians, but let's be fair about it. I would fully support a proposition limiting the total dollar amount contributed by any individual, corporation or union to $1,000 per year. With a level playing field, maybe our politicians would start to care more about us, their constituents, than the corporations currently bankrolling their campaigns. <p>
Arizona, New Mexico, North Carolina, Vermont, Maine and Massachusetts have all passed clean money legislation and the difference is astounding: In 1998, before clean money, 79 percent of Arizona's political races were decided simply by the number of dollars raised. In 2002, after clean money legislation, that number was reduced to 2 percent. <p>
While the perennial argument from the rich is that limiting political contributions limits their freedom of speech, I seriously doubt that our founding fathers wanted our First Amendment to be used as an pretext so that their children's children could be subjugated by this aristocracy based on wealth, when they fought so hard to free themselves from an aristocracy based on heredity. I think most Americans--past and present--would agree: Equality is essential for democracy. <br>
<b>Mike Kirchubel,</b> Fairfield, Calif.<p>
