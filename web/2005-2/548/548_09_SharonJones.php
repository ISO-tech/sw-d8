<font face="Times New Roman, Times, serif" size="5"><b>Getting down to Woody Guthrie</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by James York</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 17, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Sharon Jones and the Dap-Kings, <b><i>Naturally,</i> </b>released by <a href="http://www.daptonerecords.com" target="_blank">Daptone Records</a>.</font><p>

<font face="Times New Roman, Times, serif" size="3">YOU'VE PROBABLY never imagined yourself getting down to Woody Guthrie's "This Land is Your Land." Now, thanks to Sharon Jones and the Dap-Kings, it's possible. The new album <i>Naturally</i> reinvents the American classic to inspire a new generation of activists.<p>
"Most people only know the first two verses and think of it as some blindly patriotic camp song," Jones noted in a recent interview. "But Woody Guthrie wrote it as a radical protest song, although only the last few verses really hit home. Those were the ones I wanted to focus on with our version. He's asking, 'Is this land really my land?' And that question is just as relevant now, to this generation, as it was to people back then."<p>
Although "This Land" is the high point of the record politically, <i>Naturally </i>is a heavy spoonful of soul complete with funky beats and groovy rhythms that feel like Stax recordings from the early 1970s. Jones' intense vocals are a tribute to R&amp;B legends such as Tina Turner, Aretha Franklin and James Brown.<p>
She and the Dap-Kings give a new generation of activists a taste of radical music that can be funky. If <i>Naturally </i>isn't enough to satisfy, pick up the single, "What If We All Stopped Paying Taxes," an antiwar soul scorcher.<p>
"How can we talk about the price of gas/when they're stealing our brothers' and sisters' right to live/Why don't we all stop paying taxes," Jones belts out in solidarity with those dying in wars for empire.<p>

