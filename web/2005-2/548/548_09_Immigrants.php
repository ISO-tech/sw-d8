<font face="Times New Roman, Times, serif" size="5"><b>The lies they tell about immigrants</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 17, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">IN ANOTHER sign of congressional pandering to the right wing, two leaders of the Minutemen, the anti-immigrant vigilantes, received a hero's welcome when they visited Washington in April.<p>
Politicians from the Governator Arnold Schwarzenegger to Democratic Sen. Hillary Rodham Clinton have added their voices to the immigrant-bashing clamor. In these ways, Republicans and Democrats who collaborate to hand out tax cuts to the rich, and school and job cuts to everyone else, try to position themselves as champions of working Americans who, they claim, are losing out to illegal immigrants.<p>
But the idea that Latinos are living the high life while denying "native born" Americans of their jobs is simply false. In fact, according to recent analysis of U.S. Census data by the Pew Hispanic Center, Latino workers' wages declined in both 2003 and 2004 while the U.S. economy recovered.<p>
Latinos, who now represent about one in seven Americans, filled about 40 percent of the 2.5 million jobs added to the U.S. economy in the last two years. The vast majority of those newly employed Latinos were immigrants, who accounted for almost one-third of all jobs added in the U.S. economy last year.<p>
But while Latino immigrants' employment was going up, their wages were going down, Pew found. This is due in part to the heavy concentrations of Latinos, particularly immigrants, in low-skilled labor requiring minimal formal education.<p>
In contrast, 64 percent of new jobs that native-born white workers took in the same period required a college degree or more education. In fact, Pew found that Latinos are heavily concentrated in seven occupations, such as drywallers and garment pressers, in which almost 20 percent of all immigrant Latinos work, in comparison with few native-born workers.<p>
Pew concluded that while Latino immigrants and native-born workers appear to be on "different paths," "[immigrants'] growing supply and concentration in certain occupations suggests that the newest arrivals are competing <i>with each other</i> in the labor market to their own detriment." (my emphasis) This fact gives new meaning to the term "race to the bottom."<p>
Since the advent of neoliberal free-trade policies like the North American Free Trade Agreement (NAFTA), a heavily subsidized and protectionist U.S. agribusiness has helped to drive millions of Mexican small farmers off their land. Many of them are left with no other option than to move to Mexico's cities or to the U.S. for work.<p>
The Bank of Mexico recently announced that the full amount of remittances--money sent by immigrants to their families--will reach $20 billion in 2005. That's equal to the amount of money Mexico makes exporting agricultural products and amounts to almost 55 percent of its income from exporting petroleum. So the fact that the Mexican economy now makes as much from remittances as from agricultural exports is the end result of NAFTA.<p>
But in no way is this simply a one-way problem, with the Mexican government exporting its poor and, in that way, helping to maintain some level of stability in the country. Because another feature of the neoliberal world economy is the dependence of U.S. and other "core" economies on exploiting many labor forces beyond their borders.<p>
In this way, U.S. business exploitation of Chinese workers through outsourcing is another side of the same coin that sees certain U.S. industries--like the restaurant and construction industries--exploiting undocumented immigrants within U.S. borders.<p>
It's clear that when it comes to making profits, U.S. business sees no borders. That's why it's even clearer that the U.S. labor movement needs to continue to look beyond U.S. borders and to organize among immigrants as well. <p>
Improving wages and conditions for immigrants shouldn't be seen as a way indirectly to boost "American" wages and conditions separate from immigrants' conditions. On the contrary, jobs performed by immigrants are crucial to the U.S. economy.<p>
Improving their wages and conditions will benefit all workers, no matter what side of the Rio Grande (or Rio Bravo) they were born.

