<font face="Times New Roman, Times, serif" size="5"><b>Strings attached to G8 debt relief</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 17, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THE ANNOUNCEMENT of an agreement among leaders of the Group of Eight (G8) industrialized countries to grant debt relief to impoverished African nations has been hailed as a "historic breakthrough." Liberal organizations greeted the deal enthusiastically--with celebrity activist Bob Geldof declaring, "This is already a victory for the millions of people in the campaigns around the world."<p>
But the fine print tells a different story.<p>
Reached after contentious meetings between George Bush and British Prime Minister Tony Blair, the agreement calls for the International Monetary Fund (IMF), World Bank and African Development Bank to wipe out $40 billion worth of debts of 18 poor countries in Africa.<p>
During his visit to Washington last week, the Bush administration initially stiffed Blair--remaining silent on the question of debt relief and promising a meager $674 million in direct aid for Africa. The agreement on canceling the debt was only reached after Blair abandoned his proposal that wealthy nations take responsibility for repayment. Instead, the lenders themselves will now write off the loans, as Washington demanded.<p>
The amount of money that G8 debt cancellation will cost--while more than previous plans--is still a drop in the bucket for the world's richest governments. "The financial burden of the operation on rich countries would amount to some $2 billion a year, compared to $350 billion that the G8 devote to farming subsidies or $700 billion that it devotes to military expenditures," wrote the Committee for the Abolition of Third World Debt. "Rich countries are willing to spend every year for the announced cancellation half of the amount the U.S. spends every month on its continued occupation of Iraq."<p>
Then there are the strings attached to the deal. In order to qualify for debt relief, poor countries must implement strict structural adjustment programs required by the IMF and World Bank. These programs involve opening up domestic markets to imports and cuts in social spending, which usually results in more poverty--and more loans.<p>
If the G8 leaders were really interested in relieving Africa's poverty, they would stop subsidizing agribusinesses in their own countries. "African agricultural exports are $11 billion," the British <i>Observer</i> newspaper pointed out. "If the U.S. and Europe removed their farm subsidies, the value of current African food exports would double."<p>
G8 leaders claim the right to decide which African countries deserve aid because otherwise, they claim, the money would go to dictatorships that enrich themselves or build up their armies. Yet G8 countries have no problem dealing with these dictators themselves--or selling them weapons.<p>
As Ken Wiwa--son of the late human-rights activist Ken Saro-Wiwa, who was executed by the Nigerian government--argued in Britain's <i>Guardian </i>newspaper, "It has become fashionable to insist that Africa's problems are its own doing, and that violence and corruption are somehow uniquely African diseases. When the Americans preach of the need for good governance, a carrot-and-stick approach before writing off the debts of African countries, I can't help but agree, but then the sermon omits to mention the complicity of American private banks in using loans to prop up dictators."<p>
Despite the congratulatory backslapping, the G8 proposal will do little to alleviate poverty in Africa. Rather, it will reinforce a system in which the world's most powerful countries enrich themselves at Africa's expense.<p>
The problem isn't that Africa lacks the resources to provide for its population. The continent has two-thirds of the world's mineral resources. Twelve percent of U.S. oil comes from there. The problem is that the world's richest countries are calling the shots, and their priority will always be profit, not human need.<p>
That's why the demonstrators who will march against the G8 summit in Britain next month are right: Another world is possible--and necessary.<p>

