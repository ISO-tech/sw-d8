<font face="Times New Roman, Times, serif" size="4">Two-day strike forces employers to give in to key demands</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A win for LA hotel workers</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 17, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES--Chanting "&iexcl;S&iacute;, se pudo!" [Yes we did!], hotel workers celebrated their victory on June 11, two days after going on strike at the Hyatt West Hollywood.<p>
Seven LA hotels agreed to the central demands of UNITE HERE Local 11, representing 2,500 hotel workers, in a tentative agreement that includes free family health care coverage and a 65-cent wage increase.<p>
Most importantly, it also provides for a contract expiration in November 2006. Now, 15 LA hotels have agreed to a 2006 expiration date--the same year that thousands of UNITE HERE hotel workers in New York, Chicago, Toronto, Honolulu and other major cities will be renegotiating their contracts. This gives the union the chance to go to the hotel industry billionaires with a new set of demands, backed up with the potential threat of a nationwide strike.<p>
This tentative agreement didn't come easily. When the LA contracts expired in June 2004, the hotels stopped deducting union dues from workers' paychecks.<p>
Local 11 responded by organizing mass meetings in the hotel lobbies where workers paid their dues, as managers and customers looked on. As Tom Walsh, secretary-treasurer for Local 11, put it at the victory celebration, "They knew we were prepared for a two-month fight or a two-year fight, so we won in two days."<p>
When halting dues deduction didn't temper workers' demands, the LA Hotels Employers' Council (EC) imposed a $10 weekly premium for health care benefits for workers' families. To date, the hotels have sucked about $650,0000 from the workers in premiums.<p>
The union responded to this with a strike authorization vote in September and an energetic boycott campaign.<p>
The EC threatened to lock out workers at all the hotels if the union struck at just Hyatt. "Hyatt is an aggressive leader against the workers for the hotel group," Local 11 President Maria Elena Durazo told <i>Socialist Worker</i>. "They had gone so far, breaking the law [with the health care premiums], that we had to take action. The members decided to show Hyatt they're serious."<p>
The National Labor Relations Board said that it would prosecute over 50 unfair labor practice charges against the hotels, mostly concerning the health care premiums. But as <i>Socialist Worker </i>went to press, it was unclear whether employers had formally agreed to return the money or rehire UNITE HERE activists who had been wrongfully terminated during the contract dispute.<p>
Mayor-elect Antonio Villaraigosa inserted himself into the negotiations, playing a key role in brokering the pre-dawn agreement. But while he apparently did some arm-twisting with the hotel bosses, his motives had more to do with returning LA's tourism industry to business as usual. "[A lockout] would have been horrible for Los Angeles' economy and its image," he explained.<p>
This victory should pave the way to further battles for economic justice for the hotel workers. With more closely aligned contract expiration dates, UNITE HERE should be able to demand major pay increases with a continuation of free health insurance in the 2006 contract negotiations. Villaraigosa, who as a city councilman intervened against the unions in the 2003 transit strike, cannot be counted on to intervene on the workers' side in 2006.<p>
This victory came primarily because all parties could see that the membership of Local 11 was prepared to fight. The more the union relies on the strength and determination of its rank and file, the more it has a chance of winning in 2006.<p>

