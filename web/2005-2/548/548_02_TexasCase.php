<font face="Times New Roman, Times, serif" size="4">Nightmare for two teenagers</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Victim of a Texas anti-abortion law</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 17, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">GERARDO FLORES is going to jail for life--for helping his girlfriend terminate her pregnancy.<p>
Earlier this month, the 19-year-old man was convicted in Lufkin, Texas, on two counts of capital murder under a recent state "fetal protection law"--and received an automatic life sentence. Under the law, from the moment of conception, fetuses have the same rights to legal protection as a human being.<p>
The story is a nightmare that shows what can happen when a woman's right to choose abortion is restricted.<p>
In May 2004, four months pregnant with twins, Erica Basoria, Flores' then-16-year-old girlfriend, regretted not getting an abortion earlier in her pregnancy. According to Flores' defense attorney Ryan Deaton, Basoria was given incorrect advice by a doctor "that she couldn't get an abortion at that point."<p>
Flores could have sought an abortion elsewhere, but as Peter Durkin, president and chief executive officer for Planned Parenthood of Houston and Southeast Texas, told the <i>Lufkin Daily News,</i> "Clearly for a woman in Lufkin, she has to have resources to seek those services out...Abortion is getting less accessible." <p>
In a desperate effort to induce a miscarriage, the teenager began jogging and hitting herself in the stomach. Eventually, she asked Flores to help her end the pregnancy by having him step on her stomach several times over the course of several days. She later miscarried.<p>
Police and prosecutors almost immediately began to go after Flores. Given the chance, they would have tried Basoria as well--but were prevented by Texas law, which bars a woman from being prosecuted for causing the death of her fetus.<p>
Basoria stood by Flores at the trial, but prosecutors did their best to demonize him--pointing out that he had spent time previously in jail and had admitted to having a physical fight with Basoria on the day she miscarried. In a gruesome turn, prosecutors even passed around a photo of the miscarried fetuses among jurors.<p>
The forensic pathologist who performed autopsies on the fetuses admitted in court that he couldn't tell if Basoria or Flores were responsible for the miscarriage--and said that there were signs that Basoria was already at risk for a miscarriage or spontaneous abortion. But that didn't stop the jury from convicting Flores after just four hours of deliberation.<p>
Now, two teenagers' lives are destroyed--because Texas politicians wanted to score another hit against a woman's right to control her own body.<p>

