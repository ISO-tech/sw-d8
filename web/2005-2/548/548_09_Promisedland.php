<font face="Times New Roman, Times, serif" size="4"><i>Peace, Propaganda and the Promised Land:</font></i> <br>
<font face="Times New Roman, Times, serif" size="5"><b>The occupation's hidden toll</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Hadas Thier</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 17, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Peace, Propaganda and the Promised Land,</b></i> a documentary by Bathsheba Ratzkoff and Sut Jhally.</font><p>

<font face="Times New Roman, Times, serif" size="3">RECENTLY RELEASED on video and DVD, Bathsheba Ratzkoff and Sut Jhally's documentary <i>Peace, Propaganda and the Promised Land</i> delivers a scathing criticism of the U.S. media's role in misleading public opinion in regards to the Israeli occupation of Palestinian territories.<p>
The West Bank and Gaza have suffered under a brutal and illegal Israeli occupation since 1967. Unemployment in these territories is 65 percent, and checkpoints manned by the Israeli Army, and regularly shut down in collective punishment, keep Palestinians from getting to work, schools, hospitals or their homes.<p>
In fact, the West Bank was under severe closure for 66 percent of the time between 2000 and 2003. According to Amnesty International, unlawful killings, torture and ill treatment, destruction of homes, blocking of ambulances and the use of civilians as human shields are among many war crimes that Palestinians endure.<p>
Yet only 4 percent of U.S. network reports about the Israeli-Palestinian conflict even mention the fact that there is an occupation taking place! The U.S. media regularly portrays the issue as one of Israeli self-defense. Words such as "retaliation," "responds" or "strikes back," are used to refer to Israeli military operations, while Palestinian actions are summarily labeled as "attacks." <p>
Without understanding the context of the occupation, American viewers are left to believe that Palestinians fighting Israeli power are simply prone to violence and hatred, rather than resisting decades-long colonial rule.<p>
In one of many nauseating clips of media reports featured in the film, Tom Brokaw informs his audience that "Neither the best intentions of the Saudis, nor the power of Israelis could stop another young zealot willing to die so that he could kill Jews on Passover."<p>
But as Noam Chomsky explains in the film, "When Israel in the occupied territories claim that they have to defend themselves, they are defending themselves in the sense that any military occupation has to defend itself against the population they are crushing. You can't defend yourself when you're militarily occupying someone else's land...Call it what you like, it's not defense."<p>
Most importantly, the film does what the U.S. media won't--show the human toll of the occupation with heart-wrenching footage of Israeli soldiers beating Palestinians, bodies of dead civilian victims of Israeli raids, and families literally ripped away kicking and screaming as the Israeli army prepares to bulldoze their homes.<br>
Incisive interviews with left-wing journalists, academics and activists help to explain not only the workings of the occupation but also U.S. interests in the region--which leads to the $6 billion-a-year funding of Israel by the American government.<p>
The film's main weakness is that it limits its historical context to the post-1967 occupations, but leaves intact the initial creation of the Israeli state on Palestinian lands in 1948.<p>
Nevertheless, for offering a rare and refreshing dose of reality in regards to the brutality suffered by Palestinians, and for exposing the complicity of the U.S. media in its justification, <i>Peace, Propaganda and the Promised Land</i> is well worth the watch.<p>

