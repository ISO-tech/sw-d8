<font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Why did the U.S. media ignore Downing Street revelations?</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A smoking memo</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 17, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THIRTY YEARS ago last fall, Richard Nixon was forced to resign the presidency after "smoking-gun" evidence proved the full extent of the Watergate scandal.<p>
Today, George W. Bush also faces solid evidence of presidential lying--a "smoking memo" from a meeting involving British Prime Minister Tony Blair that proves the U.S. administration was determined to have its war on Iraq and prepared to "fix" the "intelligence and facts" to get it. But no one at the White House is packing their bags. Why not?<p>
The so-called Downing Street memo is the minutes of a July 23, 2002, meeting attended by Blair and top officials of his government. At the meeting--held eight months before the invasion of Iraq--the most powerful men in the British government acknowledged that, based on recent conversations with U.S. officials, the Bush administration had already decided on war.<p>
The invasion was to be justified "by the conjunction of terrorism and WMD," according to the memo, and "the intelligence and facts were being fixed around the policy." The memo describes Blair and the others considering how to approach the United Nations--not as a way to avoid war, but in the hopes that UN weapons inspections would provide a semi-legitimate pretext for invasion.<p>
None of this will surprise readers of <i>Socialist Worker </i>or other progressive publications--which exposed Bush and Blair's justifications for war as frauds before the invasion. But the U.S. mainstream media kept its collective head buried in the sand--and still does, despite the obvious credibility of the Downing Street memo.<p>
The memo caused a storm of controversy in the British and international media when it was published by the <i>Times of London </i>on May 1, shortly before national elections. But in the U.S., nothing. According to TVEyes, an around-the-clock U.S. news monitoring service, between May 1 and June 6--when Bush was questioned about the memo for the first time at a press briefing with Blair--the story was mentioned approximately 20 times on CNN, Fox News, MSNBC, ABC, CBS, NBC and PBS <i>combined.<p>
</i>None of the country's leading newspapers--not the <i>New York Times, Washington Post, Los Angeles Times </i>nor <i>Wall Street Journal--</i>devoted a front-page story or even significant coverage.<p>
This pathetic display illustrates what the mainstream media has come to represent in the Bush administration's "war on terror"-- a tool of pro-war cheerleading that Mark Danner, writing in the far-from-revolutionary <i>New York Review of Books, </i>compared to Nazi Germany's propaganda machine under Joseph Goebbels.<p>
But the media aren't the only dogs in Washington that roll over and play dead. The "opposition" Democrats have done next to nothing to step up pressure on the White House.<p>
About 100 lawmakers, led by Rep. John Conyers (D-Mich.), are calling for a congressional investigation into the memo. But this still represents a <i>minority </i>of Democrats in Congress. <p>
On the whole, Democrats in Washington accept the party establishment's line that they have to be seen as "tough" on national security--and therefore can't be associated with an antiwar stance. Thus, Bush won overwhelming approval to spend another $82 billion on the occupations of Iraq and Afghanistan. In the Senate, the vote was 100-to-0.<p>
Unfortunately, the antiwar movement has been at a standstill for much of the past year--in large part because leading voices the movement backed the pro-war Democrats in the 2004 election.<p>
The potential for building a wider movement is greater than ever today--thanks to the fact that growing numbers of people recognize, despite the media's lapdog behavior, that the occupation of Iraq is a disaster. The latest Gallup shows the highest opposition yet to the occupation of Iraq--six in every 10 people think some or all U.S. troops should be withdrawn now.<p>
The Downing Street memo vindicates what the antiwar movement argued all along--and can serve as the basis for building a wider movement. Activists at the grassroots can take a step toward this end by beginning now to organize the biggest possible turnout for a national mobilization called by several different antiwar groups for the weekend of September 24 in Washington, D.C.<p>
The "smoking memo" could yet be seen as a turning point, when a real antiwar opposition developed to confront the U.S. war on the Iraqi people. But it won't happen if we count on the politicians in Washington to take on Bush.<p>

