<font face="Times New Roman, Times, serif" size="5"><b>San Diego janitors strike for better wages and health care</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Newman</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 17, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">SAN DIEGO--Five days after going on strike, about 300 janitors marched for health care in La Jolla, Calif., North San Diego County's enclave for the super-wealthy, on June 10. As they passed the fashionable patrons of tony restaurants, marchers rattled soda cans full of gravel and chanted, "Se ve, se siente! La uni&oacute;n est&aacute; presente!" (See it, feel it! The union is here!)<p>
About 1,400 suburban San Diego janitors are members of Service Employees International Union (SEIU) Local 1877. They are the last group of union janitors in the state without company health care benefits, and most earn $7.60 an hour, or about $1,000 a month. One striker's sign asked, "Health care, food, or rent--which would you choose?"<p>
The march wound through Chancellor Park, home to many of San Diego's most profitable corporations. Strikers occupied the lobby of the main building, until police threatened arrest--and then the crowd of mostly immigrant workers rallied outside. In a display of civil disobedience, two union activists and three members of an interfaith solidarity committee were arrested when they refused to leave the lobby.<p>
The following day, downtown janitors called a one-day strike in solidarity. On June 12, members of several other unions, including the California Faculty Association, AFSCME and SEIU Local 2028, marched in solidarity.<p>
The action was designed to turn up the heat on US Metro, One Source, South Coast, San Diego Janitorial Services, and four other maintenance companies that are refusing to back off their proposal of an indefinite wage freeze with no health care benefits and cuts in sick leave. With the level of determination and solidarity this strike is seeing, these companies are feeling the pressure to grant workers the health-care benefits and wage increases that have been won by janitors everywhere else in the state.<p>

