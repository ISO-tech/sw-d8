<font face="Times New Roman, Times, serif" size="4"><b>College Not Combat</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Roger Dyer and Brian Cruz</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Antiwar activists are spearheading an effort to get an anti-military recruitment proposition on the ballot here. Just two weeks into the petitioning drive, the College Not Combat (CNC) ballot initiative already has more than half of the 10,500 signatures required to get it on the November ballot.<p>
The CNC proposition would allow San Francisco to voice its opposition to military recruiters using public school, college and university facilities to recruit young people as cannon fodder--while showing its support for scholarships and job training as an alternative to the "poverty draft."<p>
The anger that people harbor toward the war in Iraq can also be tapped into and directed at military recruiters in our schools. "The military going into every school and college actually gives us a chance to get at the war machine," said Todd Chretien, a CNC organizer.<p>
With recruiters being exposed daily for their lies and with parents and those against recruitment in our schools gaining confidence to point it out, the campaign has garnered endorsements from more than 40 groups and individuals, including antiwar organizations, veterans organizations and labor unions. Local news and radio stations have also been keeping the pulse of the CNC campaign, with the local radio station KPFA airing a weekly update on the initiative.<p>
This effort follows the overwhelming passage last November of Proposition N, by a 63-to-37 percent margin, that called on the city to urge the U.S. government to withdraw all troops from Iraq. These signs indicate that San Franciscans are ready to exclaim, "Money for jobs and education, not for war and occupation!"<p>
<i>For more information or to get involved, call 415-248-1701 or go to <a href="http://www.CollegeNotCombat.org" target="_blank">www.CollegeNotCombat.org</a></i>.<p>
