<p><a name="Abolish"></a><font face="Times New Roman, Times, serif" size="4"><b>Abolish the death penalty</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By David Buckingham</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BURLINGTON, Vt.--The federal government is seeking a death sentence in a case it plans to try here in Vermont, but anti-death penalty activists are pushing back. The indictment, originally brought by former Attorney General John Ashcroft, charges Donald Fell with three counts of murder.<p>
This trial is being used as a wedge by the federal government to re-introduce the death penalty to Vermont, a state that has not executed anyone since 1954 and which abolished capital punishment in 1987.<p>
Vermonters Against the Death Penalty has been organizing weekly pickets at the courthouse where jury selection is taking place and has gathered hundreds of signatures on an anti-death penalty petition. They are using the petition to engage with other people and encourage them to get involved with the struggle. The coalition is also organizing a public panel, bringing an exonerated death row prisoner, family members of murder victims and others to speak out against the death penalty.<p>
In 2002, John Ashcroft overturned 12 plea-bargains and turned them into federal death penalty cases, specifically targeting states that have abolished capital punishment--even as evidence mounts from across the U.S. that the death penalty is racist and has sentenced innocent people to die time and again.<p>
It's time for the United States to abolish capital punishment, but this will only be possible if we continue to organize grassroots opposition.<p>
