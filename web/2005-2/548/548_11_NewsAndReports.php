<p><a name="Abortion"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend abortion rights</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Mary Rapien</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CRANSTON, R.I.--On June 10, International Socialist Organization members and allies led a "park-in" and "sign-blocking" to keep right-wing bigots from harassing women as they entered the Women's Medical Center of Rhode Island. The clinic is one of only two in the state that provides abortion services.<p>
"Joe," an anti-abortion fanatic who has protested at the clinic unchallenged, three days a week for the past two years, was visibly perturbed to find that activists had arrived at the clinic earlier, parked in his favorite spots and set up our signs on "his" trees.<p>
With 17 people stopping by throughout the morning, we were able to block a significant number of his graphic, fake signs with our own. Our signs included slogans such as "Women welcome here," and "Pro-choice and proud." <p>
As Joe attempted to hand out literature, activists placed signs in front of him and warned people that "This man is not pro-choice. Don't listen to him." Neighbors honked in support, and several stopped by to thank us for coming out, promising to attend the next community organizing meeting to be held June 14.<p>

