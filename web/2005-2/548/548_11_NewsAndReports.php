<p><a name="Defend"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend immigrant rights</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Freddy Garcia</b></font><p>

<font face="Times New Roman, Times, serif" size="3">VICTORVILLE, Calif.--The racist anti-immigrant group Save Our State held a June 11 rally outside a Home Depot to protest its support of undocumented day laborers. Along with Save Our State, there were racists from neo-Nazi groups like Stormfront and the National Alliance at the rally. <p>
Local immigrants rights activists organized a counterprotest across the street from Save Our State's tiny event with its 19 supporters. During the protest, our side was able to bring people that were shopping or passing by to join us. As our side grew to a couple dozen, the racists finally left with a clear message--wherever they show their faces, there will be people to confront them.<p>
