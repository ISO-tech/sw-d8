<font face="Times New Roman, Times, serif" size="5"><b>NYU grad students vote on strike action</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sarah Wolf, GSOC</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 28, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--The graduate employees' union at New York University (NYU) is gearing up for a fight with the university over the life of the union.<p>
Members of the Graduate Student Organizing Committee (GSOC)/United Auto Workers Local 2110 are set to vote October 24&#8211;30 on whether to strike against NYU's refusal to recognize our union, despite petitions signed by graduate students and faculty members and demonstrations attended by thousands. If the vote results are positive, union organizers would likely call for a strike the first or second week of November.<p>
At stake in this struggle is not only the ability of the university administration to determine wages, benefits and working conditions for the graduate students who perform much of the university's teaching, grading and research, but working conditions for graduate employees at private universities across the country.<p>
In the summer of 2004, the National Labor Relations Board (NLRB) ruled that grad assistants are not workers and therefore do not have the right to unionize. Yet the large amount of teaching done by grad students and other untenured instructors only continues to increase. Indeed, as an NLRB dissenting decision stated in that ruling, "The reason for the widespread shift from tenured faculty to graduate teaching assistants and adjunct instructors is simple: cost savings."<p>
So with the full support of other wealthy private universities like Yale and Columbia--both of which have faced vibrant grad student unionization movements--NYU is at the forefront of busting grad student organizing. A victory for NYU in this situation would thus mean huge--and probably immediate--losses in material conditions and job security for grad students.<p>
In response, GSOC decided to push for a strike in mid-fall, when classes are in full swing and the opportunity exists to seriously disrupt "business as usual" at the university.<p>
Meanwhile, NYU has pulled no punches in its union-busting campaign. In addition to sending letters to students urging them not to support a strike, the administration has pressured supportive faculty to write up plans for replacing striking teaching assistants and to report their names.<p>
Despite these efforts, undergraduates have generally shown support for their teachers in class and in the student newspaper, while the faculties of several academic departments have voted to not cooperate in union-busting efforts in the event of a strike.<p>
Increasing the intensity of these shows of solidarity and waging a strike that shuts down the functioning of the university as much as possible will be the keys to winning union recognition. If we band together, convince professors to move classes off campus and ask students not to attend classes that professors have refused to move outside of university buildings, then we have a great chance of pushing NYU to the bargaining table.<p>
Holding open debates on strategy in our union and inviting direct participation of as many graduate students and supporters as possible is the way to build an effective strike.<p>

