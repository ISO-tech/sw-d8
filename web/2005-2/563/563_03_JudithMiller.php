<font face="Times New Roman, Times, serif" size="5"><b>The making of a media hack</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 28, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">IT'S FITTING that <i>New York Times </i>star reporter Judith Miller's role in whipping up support for the Iraq war has emerged amid publicity for a Hollywood movie about CBS News correspondent Edward Murrow. Murrow's confrontational 1954 interview with Sen. Joe McCarthy is often seen as one of the finest moments in the history of U.S. journalism.<p>
With even her formerly protective bosses criticizing her in the pages of the <i>Times </i>for her involvement in the White House leak scandal, Miller is being portrayed as a kind of anti-Murrow--someone prepared to serve as a stenographer for the government rather than a critic of it.<p>
That's absolutely true of her. Miller's front-page features during the run-up to the U.S. invasion of Iraq came straight from Washington's neoconservative hawks and their Iraqi stooges--from the absurd claims about Saddam Hussein's weapons of mass destruction to the rosy expectation that invading U.S. soldiers would be welcomed by overjoyed Iraqis.<p>
But it's worth remembering that Murrow ended his career as a paid government flack, running the U.S. Information Agency as Democratic President John F. Kennedy pushed the world to the brink of nuclear holocaust during the Cuban Missile Crisis and sent U.S. troops to Vietnam--all in the name of the same anticommunist crusade that McCarthy had carried out. <p>
The point of the Murrow comparison isn't to absolve Miller with an everybody-does-it excuse, but to see how the process of promotion in the corporate-controlled media inevitably leads journalists not only to proximity to the powerful, but identification with them.<p>
Miller, after all, began her career in the 1970s not as a mouthpiece for politicians and national security bureaucrats, but as the Washington correspondent for <i>The Progressive, </i>the left-wing monthly magazine. <p>
But as a <i>New York Times </i>correspondent in the Middle East in the 1980s<i>, </i>she became a reliable apologist for Israeli atrocities in the occupation of Palestinian lands--always a good career move for someone looking for access inside the Beltway.<p>
In her 1990 book, <i>Saddam Hussein and the Crisis in the Gulf</i>, produced with a co-author Laurie Mylroie during the buildup of U.S. troops before the first Gulf War, Miller was still honest enough to state, "Americans went to the Gulf for oil." But by her 1996 tome, <i>God Has 99 Names, </i>she was stoking the Islamophobia that has since become a central justification for the U.S. "war on terror."<p>
Miller, of course, isn't the first reporter to make the transition to corporate media hack. The best example is Bob Woodward, who, with Carl Bernstein, helped expose the Watergate scandal that forced the resignation of Richard Nixon. This same Woodward became a virtual White House press secretary after the September 11 attacks, churning out the book <i>Bush at War </i>to burnish Bush's image.<p>
The <i>Times </i>management may be looking for a chance to throw Miller over the side now, but they are equally responsible for the scandalous way that Washington's hawks used the "paper of record" to sell their lies. As media critic Norman Solomon put it, "The <i>Times </i>did not 'fall for misinformation' as much as jump for it. The newspaper eagerly helped the administration portray deceptions as facts."<p>
Miller scandal or not, the <i>Times </i>will be there to round up support for future wars--along with the other honored institutions of the U.S. media. They certainly aren't watchdogs, and they aren't even lapdogs. They are accomplices to the U.S. imperial project.<p>

