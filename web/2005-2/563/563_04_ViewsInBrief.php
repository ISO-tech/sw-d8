<p><a name="RC"></a><font face="Times New Roman, Times, serif" size="4"><b>Stereotypes at the Red Cross</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I'VE JUST finished reading Joe Allen's very informative article about the American Red Cross, and I have to say that I am now even more disgusted with that organization than I was before ("The truth about the Red Cross," October 21). <p>
The Red Cross came to my workplace about two weeks ago for a blood drive. When my coworker attempted to donate, she was turned away. Why? Because she had once slept with an African man. And were my gay co-workers welcome to donate and thanked for their generosity? Of course not. (This in spite of the fact that the Red Cross runs all the blood it collects through no fewer than three different tests for HIV).<p>
It would appear that in the minds of the right-wing bigots and corporate servants at the American Red Cross and Food and Drug Administration, AIDS is still the "gay plague" and the blood of all African people is tainted. <br>
<b>Jenni Rowe,</b> Baltimore, Md.<p>

