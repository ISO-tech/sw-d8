<p><a name="Cosby"></a><font face="Times New Roman, Times, serif" size="4"><b>Cosby wasn't blaming victims</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IT IS a rare day indeed when a Black person can offer "self" criticism on the state of Black America that does not provoke the knee-jerk response that the victim is being blamed ("Leading the 'blame the victim' chorus," October 7).<p>
It is as if there are only two ways out of racial oppression, and one of the ways doesn't count: there is the solution by whites or there is the self-help solution by Blacks, with the latter always ruled out as illegitimate, because it blames the victim and lets racists off the moral hook.<p>
Nonetheless, the logic of rejecting the Black self-help solutions is that only whites can legitimately and morally solve the race problem. But if you believe, as many Blacks apparently do, that many whites are incorrigibly racist, is it reasonable to expect them (who are its main beneficiaries) to solve problems of their own racism? Also, is it not absurd to put off solutions of your own that you think might work until such time as racists are able to heal themselves?<p>
But how does any of this help a 25-year-old Black mother who must take care of herself, two teens and a toddler on $18,000 a year? How does it help to tell her she must wait for solutions until the "profit is taken out of the slums" or until "we change this system fundamentally?" Is there any progress anywhere on the two issues of profit in slums and fundamental change in the system that would give this mother hope for solutions in her lifetime?<p>
Yes, perhaps you could argue that Cosby is blaming the victim if you had serious reason to doubt his intentions. I find it hard to doubt his intentions on this issue considering the millions he has given to Black universities. On the other hand, why would you interpret him that way when it is at least as reasonable and far more empowering to suggest that he is placing the possibility for everyday solutions well within the reach of ordinary parents?<br>
<b>Simon Walker,</b> Emeryville, Calif.<p>

