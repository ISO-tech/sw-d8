<p><a name="Avian"></a><font face="Times New Roman, Times, serif" size="4"><b>Military can't handle bird flu</b></font><p>

YOUR ARTICLE just gives me one more reason to detest the Shrub ("Bird Flu: The nightmare scenario," October 14). I agree with you that the military is <i>not</i> the answer to any of the threats from any illness. <p>
As to the Pentagon being the "best" agency to do the logistics, that is total crap. I did a four-year enlistment and a 13-month tour in Vietnam with the Marines. Military planning is only a bit better than the other oxymoron, "military intelligence." Our military forces are trained to fight wars--that is, to kill the "other guy." The military is <i>not</i> trained to do crowd control, disaster relief or any other such humanitarian mission.<p>
In my opinion, Shrub is just taking us all closer to a total police state. When will the rest of the American people ever wake up to this most criminal of administrations? Thank you for a most informative article.<br>
<b>Charlie Ehlen,</b> from the Internet<p>

