<font face="Times New Roman, Times, serif" size="4"><b>Inspired by September 24</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I WENT to the September 24 antiwar march on Washington, and it was an incredible experience. <p>
I was disappointed at the media (non)coverage of the protest. Tens of thousands of people, and CNN decides to focus on the 100 that came out to support King George and the war? It's ridiculous, but not surprising. We know the truth--most Americans want an immediate end to the war and occupation!<p>
I am proud to be a socialist and a member of the International Socialist Organization, and it was affirmed this weekend. <p>
It was heartening to see thousands and thousands of people converge on the nation's capital to protest this disgusting capitalist war. The left has a big (but not impossible) job ahead to bring in those who are looking for an alternative to the status quo. But we can do it! The march proved to me that we are gaining ground all over the place. <p>
Let's build on the momentum of this mass protest and keep up the dialogue and action.<br>
<b>Karen Whyte,</b> Cincinnati<p>

