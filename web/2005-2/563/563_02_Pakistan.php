<font face="Times New Roman, Times, serif" size="5"><b>More lives at stake in Pakistan</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 28, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THREE WEEKS. That's how long the U.S. and other governments have to deliver desperately needed relief to the mountainous regions of Pakistan ravaged by an earthquake earlier this month. Or hundreds of thousands more people will die.<p>
"There is a three-week window of opportunity to deliver assistance to mountainous areas before the first snowfall," the United Nation's (UN) humanitarian office reported October 24.<p>

