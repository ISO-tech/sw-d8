<font face="Times New Roman, Times, serif" size="3">The death toll from the devastating earthquake--measured at 7.8 on the Richter scale--that struck northeastern Pakistan October 8 has already reached 100,000. But hundreds of thousands more lives hang in the balance as they struggle to find food, shelter and medical attention before the freezing winter takes hold.<p>
And support from the U.S. and other major powers has been pitiful--especially considering that Washington has enormous amounts of state-of-the-art military equipment devoted to the occupation of neighboring Afghanistan.<p>
In Muzaffarbad, the capital of the part of the disputed region of Kashmir controlled by Pakistan, 70 percent of all housing was destroyed by the quake. On October 23, more aftershocks hit, measuring 6.0 on the Richter scale--among the strongest of nearly 900 aftershocks recorded since October 8.<p>
Emergency aid workers have not yet been able to reach thousands of survivors trapped in isolated mountainous areas. "It has been pouring since the morning, and I doubt anyone got any relief through," Sonia Jabbar, a social activist from Uri, told the Inter Press Service (IPS). "It's freezing cold, and even the tents and temporary tin and plastic shelters that a few villages have managed to get are proving ridiculously inadequate. The most pressing need is shelter--shelter that can withstand the elements."<p>
In Karnah, Jabbar said, "The region is already getting snowed up. Once winter sets in, Karnah is cut off for five to six months. So whatever needs to happen here must happen in the next couple of weeks on a war footing."<p>
The conditions that survivors are forced to endure--living in overcrowded quarters and depending on water contaminated by dead bodies and animal carcasses--make them easy prey to any number of diseases. "If we don't act fast there are chances of widespread diarrhea, cholera, typhoid and measles. Even polio, a disease that had almost been eradicated may resurface," Qaisar Sajjad, general secretary of the Pakistan Medical Association in Karachi, told the IPS.<p>
In mid-October, Bush grabbed some headlines with a visit to the Pakistani Embassy in Washington to offer his sympathy. But the devastation of the earthquake has all but dropped out of the pages of the American press obsessed with tracking Hurricane Wilma's descent on the Florida coast. <p>
Once again, Washington's warped priorities have been laid bare.<p>

