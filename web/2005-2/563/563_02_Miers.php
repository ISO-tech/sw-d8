<font face="Times New Roman, Times, serif" size="4">White House scandals put Supreme Court confirmation in question</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Will Miers be defeated?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 28, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">SENATE JUDICIARY Committee hearings were set to begin November 7 for Harriet Miers, George W. Bush's nominee to replace retiring Supreme Court Justice Sandra Day O'Connor.<p>
Early on, Miers was hailed by Democrats like Sen. Chuck Schumer (D-N.Y.) and anti-choice Senate Minority Leader Harry Reid for her potential to appeal to both sides of the aisle. <p>
But as the confirmation process for Miers--a long-time Bush ally and Christian evangelical who has never been a judge--has worn on amid high-profile scandals in the Bush White House, her shoo-in status is in question. "If you held the vote today, she would not get a majority either in the Judiciary Committee or the floor," warned Schumer on NBC's <i>Meet the Press, </i>highlighting Senate Republicans' uncertainly over Miers.<p>
The debate over Miers is a stark contrast to the confirmation of John Robert as chief justice this summer. Then, Republicans argued that the conservative Roberts--who like Miers is a close friend of the president--didn't have to divulge his personal position on any number of issues, including women's right to abortion.<p>
Before the quick and easy nomination process had even begun, Democrats like liberal Sen. Ted Kennedy (D-Mass.) had promised not to apply an abortion "litmus test." The hearings went pretty much as planned, with the nominee responding with a series of vague answers. Roberts sailed through, taking the chief justice spot created by William Rehnquist's timely death.<p>
Enter Harriet Miers. Now Republicans--up in arms about whether Miers will be conservative enough--are demanding that she explain her positions. Sens. Lindsey Graham (R-S.C.) and Sam Brownback (R-Kan.), breaking from the administration's claims about the importance of executive privilege, are leading the call for the White House to hand over internal documents related to Miers' service as White House counsel.<p>
"If you're a conservative, the strongest argument for her is, 'Trust Bush; he knows what he's doing,'" complained William Kristol, editor of conservative the <i>Weekly Standard</i>. "I don't think that's a strong argument." The <i>Standard </i>and other right-wing publications like the <i>National Review </i>have called on Bush to withdraw Miers' nomination.<p>
Despite this, Bush insists that he'll go to the wall for this nomination--and will refuse to release any White House legal documents written by Miers.<p>
But certain documents have been released to the media--to assuage anti-choice conservatives. According to press reports, during a 1989 campaign for the Dallas City Council, Miers promised that she was staunchly opposed to abortion. Asked in a Texans United for Life questionnaire whether she would support legislation restricting abortions if the Supreme Court allowed it, Miers said she would. She also backed a Constitutional amendment to ban abortion in most cases and promised to appear at "pro-life rallies and special events."<p>
Before that, right-wing evangelical James Dobson of Focus on the Family publicly revealed that he'd gotten assurances about Miers from Deputy White House Chief of Staff Karl Rove. "When you know some of the things that I know--that I probably shouldn't know--you will understand why I have said, with fear and trepidation, that Harriet Miers will be a good justice," Dobson said on his radio show October 5.<p>
If all this is meant to assure conservatives, it should be setting a fire under the Democrats. With half of Bush's allies occupied with indictments and scandals, and the other half trying to prop up a disastrous war in Iraq, the Democrats couldn't ask for a better opportunity to crush the nomination of another right-wing justice.<p>
But if Roberts' trouble-free confirmation is any indication, don't bet on it.<p>

