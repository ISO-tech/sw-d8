<font face="Times New Roman, Times, serif" size="5"><b>Students take on religious right in Wichita</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 28, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">A GROUP of Wichita, Kan., high school students turned the tables on the anti-abortion right October 10. For three hours, dozens of demonstrators, organized by students from the city's West High School, protested outside Spirit One Christian Center. <p>
The protest was called after the anti-choice group Operation Save America, sponsored by Spirit One, set up graphic pictures of a supposedly aborted fetus and handed out anti-choice pamphlets on the campuses of West High and Wichita State University. The group not only condemned abortion, but also declared that "homosexuality is a sin and Islam is a lie."<p>
"It really shook us up," Louis Goseland, a senior at West High, told the <i>Wichita Eagle</i>. "Everybody in my algebra class was kind of sidetracked talking about that and how much they disapproved of it and how much it made them angry."<p>
So Goseland and others decided to confront the bigots on their own turf. Students from North, Northeast and Southeast High Schools and Wichita State, as well as members of the city's Peace and Social Justice Center, joined in the protest at the church. They carried signs, and some brought rainbow flags--symbols of gay pride. "We will not tolerate their intolerance," Goseland explained. <p>
When Rusty Thomas--a minister affiliated with Operation Save America--came out and lectured protesters that "Jesus Christ has the power to set us free from the sin," the crowd of demonstrators responded by yelling "Stop the hate!" <p>
"These people seem to force their opinions on others," West High junior Myles Bone, told the <i>Eagle</i>. "Everyone should be able to make their own opinions on whatever they do." As the demonstration ended, a group of students remained, clapping their hands and singing, "All we are saying is stay out of our schools."<p>

