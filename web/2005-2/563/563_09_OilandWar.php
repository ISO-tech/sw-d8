<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Is the Iraq war just about the oil?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 28, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">"IF WE are to reduce the incentives for war," a recent Global Exchange letter promoting a campaign for more fuel efficiency in autos argued, "it is imperative that we break our dependence on oil." This is a commonly held view in the antiwar movement<i>.<p>
</i>There can be no doubt that the U.S. invaded Iraq because the region sits on the world's largest reserves of oil--more than double the proven oil reserves of the <i>rest of the world</i>.<p>
The U.S. also considers Africa, after years of "malign neglect," to be strategically important because of its oil, and has had a more interventionist stance toward the continent as a result. And yet, the U.S. is not entirely dependent on the Persian Gulf for its oil consumption. Though it imports 56 percent of its oil, the U.S. only gets about 12 percent from the Persian Gulf. The largest oil exporter to the U.S. is, in fact, Canada.<p>
The U.S. did not go to war because it needed Iraq's oil; it went to war to gain greater control over oil as a strategic resource.  When it comes to any important resource, the biggest powers want to guarantee that they have both access and control.<p>
Even if the U.S. produced all of the oil consumed in the U.S domestically, it would still seek to gain as much control over oil as it possibly could, if only to prevent other rising powers from exercising that control.<p>
But if suddenly world capitalism began running its businesses and militaries on something other than oil, would that end, or at least lessen, the threat of war? If only it could be so easy.<p>
The U.S. has launched many wars that were demonstrably <i>not</i> about oil. Vietnam and the countries surrounding it are not oil-rich. Yet the U.S. conducted a devastating war there that lasted more than a decade.<p>
U.S. Marines have landed on Central American soil dozens of times in order to seize customs houses, take control of national banks, remove leaders who defied them and put in power clients who answered to them. None of these interventions were about oil.<p>
Then there is Bush's saber-rattling about preventing "rogue nations" from acquiring nuclear weapons, or Rumsfeld's complaints about China's growing military budget. What does this tell us? The <i>central</i> feature of imperialism is not competition between states for resources, but competition for markets and profits.<p>
Often, the U.S. will go to war not because that particular country is of central economic or strategic importance, but because not acting will be seen by its rivals and allies as a sign of weakness. No ruler or country, no matter how weak, small, or economically insignificant, therefore, can be allowed to defy the U.S. and get away with it.<p>
The world economy is so intertwined that no nation can retreat into its own borders and produce everything it needs. But the way in which the world's resources are distributed is not through rationally planned sharing, but through a constant struggle, hidden and open, to determine which great power is to dominate the world economy. <p>
As the Russian Marxist Bukharin wrote in 1916: "The development of world capitalism leads, on the one hand, to an internationalization of the economic life and,...to an infinitely greater degree, the same process of economic development intensifies the tendency to 'nationalize' capitalist interests, to form narrow 'national' groups armed to the teeth and ready to hurl themselves at one another any moment."<p>
There are many specific reasons that a nation goes to war in a particular circumstance. But we must step back and look at the overarching relations in the world system between competing powers to understand the dynamics of war.<p>
Washington's own strategic documents outline its main concern: to prevent the emergence of a peer competitor. A reduction in the world's dependency on oil will not change the dynamics of world capitalist rivalry that is the seedbed of wars.<p>

