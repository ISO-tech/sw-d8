<font face="Times New Roman, Times, serif" size="4"><b>Military out of our schools</b></font><p>

<font face="Times New Roman, Times, serif" size="3">As U.S. casualty figures from the war in Iraq neared the 2,000 mark, students across the U.S. continued to speak out against the war on their campuses.<p>
-- In <b>Seattle, </b>150 students, parents, teachers and community members gathered at Ranier Beach High School October 23 for a teach-in against militarism in public schools sponsored by the American Friends Service Committee, Sound Non-Violent Opponents of War, the People's Institute, the International Socialist Organization (ISO) and more. <p>
Last year, Seattle's Garfield High School Parent-Teacher-Student Association (PTSA) became the first in the U.S. to demand an end to military recruiters in public schools. <p>
Keynote speaker Rev. Robert Jeffrey of the New Hope Baptist Church pointed out that militarism causes poverty by putting resources into destruction. "There would be no war in Iraq if this country was not controlled by the rich and powerful. We need to shut down those who feed off the fear and hopelessness that exists in society!", said Jeffrey.<p>
Amy Hagopian, president of the Garfield PTSA urged other parents, students and teachers to get their PTSAs to pass counter-recruitment resolutions. ISO member Vicky Jambor quoted antiwar activist Cindy Sheehan, saying, "Fifty-eight percent of the American public are with us. We're preaching to the choir, but not everybody in the choir is singing. If all of the 58 percent started singing, this war would end."<p>
In one workshop, the mother of a Marine said, "If my son died, they would call him a hero. He would be a hero--but I don't want a hero. I want my son home."<p>
On November 2, students from high schools and colleges all over the Seattle area will protest the war by walking out of class and converging on Westlake Mall at noon.<p>
-- In <b>Austin, Texas, </b>30 people from the Campus Antiwar Movement to End the Occupation organized a protest against military recruiters on October 19 at the University of Texas Career Expo. The group surrounded the Army recruiters, and hoisted signs reading, "When recruiters lie, students die," and "Recruiters off our campus." <p>
Protestors chanted military-style cadences, including: "When they send you off to war, they don't tell you what's in store. Killing civilians is a crime, from Iraq to Palestine." The group was very successful in keeping people away from the recruiters.<p>
-- In <b>Santa Cruz, Calif., </b>50 students gathered at the Quarry Plaza of University of California-Santa Cruz October 18 in protest of the military recruiters at the university's job fair. Students marched to the University Center where the job fair was being held, gaining more students on the way, numbering approximately 100 by the time they approached the University Center. Students chanted and listened to speakers talk about the discrimination in the military, the war in Iraq and the illegal Israeli occupation of Palestine. <p>
More than 20 students also lined up to directly ask recruiters about the military's "don't ask, don't tell" policy. Same-sex couples also held a kiss-in in front of the recruiters' table. The recruiters failed to recruit a single student. <p>
<i>Steve Leigh, Matthew Beamesderfer, Cyndi Gacosta and Hector Agredano contributed to this report</i>.<p>

