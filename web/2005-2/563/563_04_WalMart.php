<font face="Times New Roman, Times, serif" size="4">What the retailer did and didn't do after Katrina</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Fixing Wal-Mart's image</b></font> <p>
<font face="Arial, Helvetica, sans-serif" size="2">October 28, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">RECENTLY, ABC's <i>World News Tonight</i> examined Wal-Mart's role in disaster relief in the Gulf states in a special segment called "What Can Wal-Mart Teach FEMA About Disaster Response?" Even by the low standards of corporate journalism today, ABC's story was so superficial and laudatory it must have come straight from the p.r. department of Wal-Mart.<p>
Here's just one excerpt from "A Closer Look" reproduced on the ABC News Web site: "With Wal-Mart's smooth response to Katrina and Rita, some are beginning to ask why government agencies can't perform as well as a discount retailer. Companies like Home Depot, Lowe's Home Improvement and the Waffle House restaurant chain have all been noted for their rapid ability to provide relief in disaster."<p>
Now aside from the obvious questions like "Do you want a Waffle House in charge of saving people's lives" or "Couldn't anybody have jumped over the low bar set by the buffoonish ex-FEMA Director Mike Brown?" one could ask, aren't heavy campaign contributors to the Republican Party, like Wal-Mart, somewhat responsible for the inept response of the Bush administration?<p>
After all, FEMA, never a great agency to begin with, has been bled white under Bush and the Republican-controlled Congress, and Wal-Mart bears a lot of responsibility for keeping these people in power.<p>
Since 2000, Wal-Mart has contributed over half a million dollars in "soft money" donations to the Republican Party and made significant contributions to the 2004 re-election campaigns of House Speaker Dennis Hastert (R-Ill.), ex-Majority Leader Tom DeLay (R-Texas), current Majority Leader Roy Blunt (R-Mo.) and President George W. Bush. That's not all. In the 2004 races for the House of Representatives, Wal-Mart donated over $1 million dollars to House Republican candidates and $200,000 to Senate Republican candidates.<p>
In many ways, Wal-Mart is the government in power.<p>
One could also ask, "Wasn't Wal-Mart's stepped-up response to Katrina, after initially cutting off its employee paychecks, an attempt at burnishing its sagging public image?" As reported last July in the <i>Wall Street Journal</i>: "The activist group Wal-Mart Watch says its polling shows the company's favorable rating dropping to 50 percent from 59 percent in March."<p>
Most of the decline is the direct result of a joint campaign by unions and community groups to expose the dark side of the world's largest retailer. This led Wal-Mart CEO Lee Scott to whine: "We're the focus of one of the most organized, most sophisticated, most expensive corporate campaigns ever launched against a single company." To Wal-Mart, Katrina was an opportunity to try to turn around its image.<p>
Most of Wal-Mart's "smooth and efficient" disaster relief was brought to the Gulf states by truck. ABC, however, failed to notice that a lawsuit has been brought against Wal-Mart by a former Black yard driver for discriminating against African Americans in hiring on-the-road truck drivers. While 15 percent of on-the-road drivers across the country are African American, only 2 to 3 percent of the drivers for Wal-Mart's 10,000-truck fleet are Black.<p>
Last, but not least, if you go to the ABC News Web site, you can't help but notice that a major advertiser is--you guessed it--Wal-Mart! Not looking at all seems to be the approach of this "closer look."<br>
<b>Joe Allen, </b>Chicago<p>

