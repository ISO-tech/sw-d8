<font face="Times New Roman, Times, serif" size="4">Working-class women are the stars of <i>North County</i></font> <br>
<font face="Times New Roman, Times, serif" size="5"><b>Standing up to sexual harassment</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Jennifer Roesch</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 28, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>North Country,</b></i> directed by Niki Caro, written by Michael Seitzman, starring Charlize Theron and Frances McDormand.</font><p>

<font face="Times New Roman, Times, serif" size="3">FOR THOSE of us fed on a steady diet of "post-feminism," <i>North Country</i>--harrowing as it is--provides a welcome antidote.<p>
It is the story of the first class-action sexual harassment lawsuit in this country. But, as importantly, it is about the reality of sexism and women's oppression today, particularly as it affects working-class women.<p>
In 1974, the mining companies in the Iron Range of Northern Minnesota signed a consent decree allowing women to work in the mines for the first time. Previously they had only been allowed access to low-paid, non-unionized "pink collar" jobs as secretaries and clerical workers.<p>
In 1975, the first women began working at Evelyth Mines. They were subjected to a campaign of sexual harassment and intimidation by male coworkers and supervisors. The company turned a deaf ear to women's complaints, encouraging them to quit if they couldn't take it.<p>
In 1984, one of the women--Lois Jensen--decided to file a lawsuit. The company waged a decade-long fight against her, spending $15 million and employing a "nuts and sluts" defense in which they exposed every aspect of Jensen's sexual history and alternately accused her of imagining the harassment or encouraging it. In the courtroom, she suffered as humiliating a sexist assault as she had in the mines. The case was finally settled just eight years ago in 1997.<p>
While it condenses the time frame and adds a standard Hollywood courtroom climax, <i>North Country </i>remains true to the essence of this story.<p>
Charlize Theron plays Josie Aimes--a single mother of two who leaves an abusive marriage and returns home to try and get back on her feet. She already has a bad reputation as someone who got pregnant in high school, had a son and says she doesn't know who his father is. <p>
Her parents believe that she should work things out with her abusive husband. But she is determined to start over and provide for her children herself.<p>
An old friend, Glory, tells her about jobs at the mines, where the work is hard and dangerous but pay is six times more than any other job a woman could get in the town. For Josie, it's the only path to a financially secure and independent life.<p>
In recent years, the issue of sexual harassment has frequently been written off as "political correctness" and women not having a sense of humor. This movie shows the reality of a hostile workplace. Women find dildos placed in their lunchboxes; they are leered at and fondled; and, they are the objects of a seething anger and constantly called sexist names and told they are not wanted. <p>
In an early scene, the movie explains the fear and anger that the men have about women taking jobs in the mines. Josie, horrified by her treatment, complains to her supervisor. It is the midst of a recession, and he explains that steel mills are closing down, and there is a lot of job insecurity. He tells her, "You girls are taking jobs that aren't there. Nobody wants you here."<p>
The movie also shows, though, the impact on women of being able to have well-paying jobs that allow them independence. Josie's first week at work is both physically and psychologically exhausting. But she tells Glory how freeing it was to cash her own paycheck, describing it as "like living for the first time." She is able to buy her own home, buy hockey skates for her son and take her kids out for a restaurant meal.<p>
Eventually, Josie is unable, or refuses, to take the harassment at work and speaks out. When she does so, the workplace becomes even more dramatically polarized. The level of sexist abuse increases dramatically. And many of her female coworkers, even her friend Glory, conclude that it is better to stay silent.<p>
Josie becomes reviled throughout the community, and her children become ostracized. When she files a lawsuit, many believe that it will shut the mines down.<p>
But as Josie continues to fight, it forces those around her to take a side. Her parents and many of her coworkers begin to change their views. And the women she works with have to decide whether to join Josie's lawsuit. The movie builds to a somewhat forced conclusion, but nonetheless shows the impact that one person fighting back can begin to have.<p>
The power of this movie is that it places its main storyline in the broader context of a society that is structured around women's oppression. And it places the struggle of a working-class woman at its center.<p>
It's been too long since a movie of this type has been made; and we are way past due for a discussion of these issues. <i>North Country </i>is a valuable contribution to beginning that discussion.<p>

