<p><font face="Times New Roman, Times, serif" size="4"><b><i>SW</i> distorted CP's record</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I TAKE issue with some things in  Paul D'Amato's article "The CP adapts to the right...again" (July 22).<p>
The author spends the majority of the article stringing together quotes from historical leaders of the Communist Party USA to paint a picture of a party that has moved "rightward" for the past 70 years. The article nowhere states that the CP long ago repudiated Browder, Stalin and the internment of Japanese Americans.<p>
D'Amato summarizes the recent National Convention of the CPUSA as "reaffirming its support for the Democratic Party." In fact, the CPUSA recommitted itself to a militant fightback against the corporate assault on jobs, rights and unions. It rededicated itself to the struggle against the occupation of Iraq and to the building of an all-people's alliance against the ultra-right forces that dominate American political life. <p>
Most importantly, the convention adopted a new party program rooted in the Lenin's concept that "the more powerful enemy can be vanquished only by exerting the utmost effort and by the most thorough, careful, attentive, skillful and obligatory use of even the smallest rift between the enemies, any conflict of interests among the bourgeoisie of the various countries and among the various groups or types of bourgeoisie within the various countries, and also by taking advantage of any, even the smallest, opportunity of winning a mass ally, even though this ally is temporary, vacillating, unstable, unreliable and conditional."<p>
Finally, the article claims <i>Political Affairs</i>, the CP's magazine asserts that gay marriage is "too much, too early." In fact, the party supports gay marriage. D'Amato is referring to a guest opinion article by a non-party member--part of a dialogue on strategies for gay rights, which posed the strategic <i>question</i> "Gay Marriage: Too Much, Too Soon?"<p>
You can disagree with the CPUSA, but please don't stretch the truth to make your point. We welcome your readers to examine or Convention materials at <a href="http://www.cpusa.org" target="_blank">www.cpusa.org</a> to see what the CPUSA really believes.<br>
<b>Libero Della Piana,</b> Communications Director, CPUSA<p>

