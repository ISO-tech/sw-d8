<p><a name="Lying"></a><font face="Times New Roman, Times, serif" size="4"><b>They're lying and spying</b></font></b></font><p>

<font face="Times New Roman, Times, serif" size="3">RECENTLY, THE media have reported on two stories that show why Americans should always question the motives and honesty of the politicians who control our country. <p>
First, the Transportation Security Administration (TSA) has been collecting personal information on some airline passengers. So what's the problem? Well, Congress said they couldn't do it, and TSA officials said they wouldn't do it.<p>
The other story is that the Justice Department has denied that it is interested in the reading habits of library patrons. So what's the problem? Surveys have revealed that since 2001, law enforcement agents have requested checkout records from libraries more than 200 times. So have government employees decided that it is okay to go against government policy, or has the government decided that it is good policy to routinely lie to the American people?<br>
<b>Chuck Mann</b>, Greensboro, N.C.<p>

