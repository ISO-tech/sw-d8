<p><a name="Unionists"></a><font face="Times New Roman, Times, serif" size="4"><b>Message of Iraq unionists</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IN RESPONSE to "Iraqi Trade Unionists Tour the U.S." (June 24), I believe that the antiwar and labor movements should have put <i>more</i> resources into the tour of the Basra General Union of Oil Employees. <p>
Many unions in Los Angles, with the notable exception of the United Steelworkers, did not invite the Iraqis to speak to their memberships. For different reasons, the biggest antiwar group in Southern California, International ANSWER, decided not to participate in the tour and invite its supporters.<p>
This was a real shame, since the driving message behind Hassan Juma'a Awad and Faleh Abbood Umarra's presentation was that the U.S. occupying force <i>is</i> the problem. "In Iraq, we are brothers," said Juma'a Awad. "Brothers can go to war, but then brothers make peace. Give us a chance to make our own country." Hearing this unapologetic anti-occupation message from Iraqis shifted many unionists. <p>
The GUOE leaders made their presentation especially effective by making the connection to privatization as a global problem that affects us all. They also told inspiring stories of their "Unscrew the Screws" work slowdown action against Halliburton subsidiary Kellog, Brown &amp; Root (KBR), their three-day strike in 2004, and their successful campaign to stop KBR from privatizing the oil industry and contracting out work.<p>
In response to a question (not as part of their presentation), I heard the GUOE leaders criticize the particular tactics of the Zarqawi wing of the Iraqi resistance. I support their right to do so. They also made a vague statement about the targeting of Iraqis with car bombs. But it seemed to me that everything that they said was in the spirit of figuring out how best to end the U.S. occupation.<p>
Americans have a lot to learn from the GUOE, both in terms of how to fight privatization and in terms of why the occupation should end now. I wish more organizing would have been done so that many more of us could hear their stories. <br>
<b>Sarah Knopp,</b> Los Angeles<p>

