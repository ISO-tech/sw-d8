<p><a name="Needle"></a><font face="Times New Roman, Times, serif" size="4"><b>Space Needle restaurant</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Ellie Fingerman</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--The Space Needle, a longtime symbol of this city, has begun to symbolize corporate greed. Nearly  200 restaurant employees,  members of UNITE HERE Local 8, have been negotiating for over two months for a new contract with a management team that's determined to break the union.<p>
Over the past year, the Space Needle restaurant grossed over $13 million in food sales alone. Yet instead of recognizing the role of workers in creating this wealth, the corporation has thrown insult after injury in each step of contract negotiations. <p>
The first signal that they were going to fight rough was the hiring of a lawyer from a prominent, nationally known union-busting law firm. The Web site of the firm boasts of assisting "many employers in winning NLRB elections or in avoiding union elections altogether."<p>
Management used stalling tactics from the beginning, not even putting forward a proposal until a month after the contract expired, and offering no dates available to negotiate. <p>
And the proposal is a slap in the face. It includes a mere 15 cents per hour raise and increases in health care costs. It also maintains a barbaric discipline policy, omits an immigrant workers' rights clause, implements random drug testing and takes away freedom of speech by denying the right to wear a union button and allowing management to censor the union bulletin board.<p>
This is an important labor struggle in Seattle because of the prominence of the Space Needle and the confidence that a successful contract would bring to workers around the state.<p>
UNITE HERE has done a good job of leafleting and involving community organizations--but more focus is needed on the workers themselves. If workers can be mobilized around the key issues, management would be brought to its knees.<p>

