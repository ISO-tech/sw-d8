<p><a name="WalG"></a><font face="Times New Roman, Times, serif" size="4"><b>Walgreens</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Glenn Allen</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--Walgreens pharmacists in the Chicago area voted to return to work July 25 without a contract, ending a three-week walkout  without a contract. Nearly half the union members were crossing the picket lines when the strike ended.<p>
The strike took place despite an offer of an approximately 20 percent pay increase over four years. Demands centered around adequate break times and employee input into staffing levels. Pharmacists at busy stores routinely fill more than 20 prescriptions an hour in addition to other duties.<p>
The key issue was Walgreens attempt to break the union, the National Pharmacists Association (NPhA), with an end to dues paycheck deductions and elimination of the closed union shop. In fact, union members have already received e-mails explaining how to decertify the union.<p>
The NPhA has formed a strategic alliance with the United Steel Workers union, which represents more than 3,000 pharmacists at Safeway and Kroger. The USW intends to continue a publicity campaign highlighting Walgreens' anti-union policies, but no talks have been scheduled with the company.<p>

