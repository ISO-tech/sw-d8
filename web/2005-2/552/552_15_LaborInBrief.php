<font face="Times New Roman, Times, serif" size="4"><b>Madison Market Co-op</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Darrin Hoop, UFCW Local 1105</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--The contract fight at Madison Market Co-op is coming to a head with a contract vote August 4. Sixty workers, members of United Food and Commercial Workers (UFCW) Local 1105, will be voting on a contract that is not recommended by the three-person rank-and-file negotiating team. <p>
The contract expired August 1, 2004. Since then, a new general manager and a store manager were hired with the promise to treat workers better.<p>
Yet soon afterward, one negotiating team member, Suzie True, was wrongfully drug-tested and suspended without pay until the results came back negative <p>
Despite an effort to recall the negotiating team last December, the union has achieved gains in the proposed contract language--for example, a ban on drug testing. All lower-paid courtesy and helper clerk positions would be eliminated, and those workers would automatically be moved into the higher paid classifications.<p>
Other gains include a limit on five consecutive mandatory workdays, two consecutive days off upon demand, two weeks of paid paternity leave after one year on the job and a maximum of six paid weeks after six years. The proposed contract allows a second shop steward and the inclusion of three workers on the five-person "leadership team" that sets store polices. <p>
Nevertheless, the negotiating team is recommending a "no" vote because management is offering almost no financial gains. They're offering a raise of only 30 cents an hour to those workers who've worked over 7,280 hours--roughly three and a half years, with retroactive pay only going to June 1, 2005. <p>
The union is asking for a three-year contract retroactive to August 1, 2004, with a 3 percent raise. We want another 3 percent starting August 1, 2005 and a 4 percent raise in the final year. <p>
Management's wage proposal amounts to only $19,000 a year--compared to the store's $80,000 annual budget for legal and professional services. We should vote no August 4 to send a message to the Co-op that we will not allow them to divide the staff by only giving raises to the highest paid workers. <p>

