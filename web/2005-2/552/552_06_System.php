<font face="Times New Roman, Times, serif" size="4"><b>Tancredo's terror plot</b></font><p>

<font face="Times New Roman, Times, serif" size="3">REP. TOM Tancredo's (R-Colo.) plan for fighting the U.S. "war on terror" is to "take out" Islamic holy sites.<p>
Appearing recently on WFLA-AM in Orlando, Fla., Tancredo was asked by talk show host Pat Campbell how the U.S. should respond if terrorists struck U.S. cities. "Well, what if you said something like--if this happens in the United States, and we determine that it is the result of extremist, fundamentalist Muslims, you know, you could take out their holy sites," Tancredo answered.<p>
"You're talking about bombing Mecca," Campbell said. "Yeah," Tancredo responded.<p>
Tancredo's spokesman defended him by saying that the representative was only speaking "hypothetically." "We have an enemy with no uniform, no state, who looks like you and me and only emerges right before an attack," spokesman Will Adams said. "How do we go after someone like that? What is near and dear to them? They're willing to sacrifice everything in this world for the next one. What is the pressure point that would deter them from their murderous impulses?"<p>
Tancredo himself later said he was "just throwing out some ideas," but that an "ultimate threat" might have to be met with an "ultimate response." He refused to apologize, but has since issued a statement of "clarification" that reads, in part, "Much more thought would need to be given to the potential ramifications of such a horrific response."<br>
-- <b>Associated Press</b>, July 18, 2005<p>

<font face="Times New Roman, Times, serif" size="4"><b>This school brought to you by...</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WELCOME TO the Plymouth-Canton Elementary School, presented by Comerica. That could be the reality in the Plymouth-Canton, Mich., school district, where administrators say they're so strapped for cash that they had to approve the selling of naming rights to their new elementary school, existing schools, athletic field and special events.<p>
A growing number of school districts nationwide have or are setting up policies on how to handle corporate gifts that are attached to a name. So far, it has shown up in small ways: a pair of school playgrounds in Rochester, Mich., named after competing homebuilders or an outdoor classroom in Walled Lake, Mich., named for Lowe's.<p>
But other school districts are breaking new ground. The Brooklawn school district in New Jersey has auctioned off naming rights to schools and other facilities on eBay. Grapevine-Colleyville school district in Texas offered ad space on school buses, stadiums and a middle school roof. Philadelphia's school system said it would consider offers for the names of several of its new high schools.<p>
The Plymouth-Canton policy doesn't spell out how much money a school name would cost. Tom Sklut, the district's chief development officer, said the industry standard is 51 percent of a new building's cost. In the case of the new $15 million elementary school, that would mean a donation of more than $7.5 million.<br>
-- <b>Associated Press</b>, July 10, 2005<p>

