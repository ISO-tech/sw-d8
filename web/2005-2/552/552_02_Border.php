<font face="Times New Roman, Times, serif" size="4">Vigilantes force immigrants to hide longer in the desert</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Record deaths at the border</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By William Figueroa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 5, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">AT LEAST three immigrants a day died in the opening weeks of July trying to cross the desert at the Mexico-Arizona border. The total number of immigrants found dead in July after trying to make the trek through this forbidding terrain was the most ever counted in a single month.<p>
Since tougher border policies like Operation Gatekeeper were implemented in the 1990s, an estimated 3,000 migrants have lost their lives on the U.S.-Mexico border. And racist vigilante groups like the Minutemen have made it their mission to make matters worse, forcing people to hide longer in isolated desert regions.<p>
Yet earlier this month, Federal Customs and Border Protection Commissioner Robert Bonner announced that his agency might look into forming "something akin to a Border Patrol auxiliary" of citizen volunteers to help watch the U.S.-Mexico border.<p>
In response to recent border actions by groups like the Minutemen, Bonner told the Associated Press on July 20, "It is actually as a result of seeing that there is the possibility in local border communities, and maybe even beyond, of having citizens that would be willing to volunteer to help the Border Patrol." After Bonner's remarks caused an uproar, the Department of Homeland Security retreated and said there were no plans to form a border patrol auxiliary.<p>
July marks the start of the so-called "season of death," when temperatures spike to over 100 degrees.<p>
Humanitarian aid groups have increased efforts to resolve the humanitarian crisis unfolding along the border, but they have met resistance. On July 9, two No More Deaths volunteers were arrested by the U.S. Border Patrol while evacuating three people suffering medical distress from the desert near Arivaca, Ariz. Two weeks later, in Campo, Calif., a pregnant woman who went into labor while crossing was denied assistance and threatened by Minutemen, while human aid workers struggled to get her medical care.<p>
Rev. Robin Hoover, director of the humanitarian group Humane Borders, said the death toll points to a failure in policy. "We've known that the Border Patrol strategy is not working," Hoover said. "This just shows us that they have no idea how to reduce the deaths."<p>
The struggle of immigrants brutal conditions continues farther away from the border in the case of migrant farmworkers. Three men died after working in the recent intense heat of California's Central Valley as workers move up and down the state looking for work in the fields.<p>
In mid-July, Salud Zamudio-Rodriguez couldn't keep up with the tractor that was dictating his pace in a bell pepper field near Arvin, Calif. Coworkers told the <i>Los Angeles Times </i>that for more than two hours, the tractor doubled its speed and forced pickers to work faster, despite temperatures that reached 105 degrees. The 42-year-old Zamudio-Rodriguez collapsed from heat exhaustion. "We watched him dying in the field," coworker Soledad Reyes told the <i>Times.<p>
</i>Two more farmworkers died earlier in the San Joaquin Valley, after working in temperatures of about 108 degrees. The United Farm Workers union marched through Arvin on July 29, demanding better safeguards for workers.<p>
The U.S. government's racist immigration policies are to blame for these deaths.<p>

