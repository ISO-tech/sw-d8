<font face="Arial, Helvetica, sans-serif" size="2"><b>NORTHERN IRELAND</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Behind the IRA's disarmament plan</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Shaun Harkin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 5, 2005 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">THE IRISH Republican Army (IRA) has announced an official end to its armed struggle to end British rule in Northern Ireland. <p>
Seanna Walsh, a veteran Republican activist and former cellmate of hunger striker Bobby Sands, who starved to death in prison in 1981, read a prepared statement July 28, declaring, "The leadership of Oglaigh na hEireann has formally ordered an end to the armed campaign. All IRA units have been ordered to dump arms. Volunteers must not engage in any activities whatsoever." The IRA also announced its commitment pursue its objectives through "peaceful and democratic" means. <p>
The IRA declaration was hailed by British Prime Minister Tony Blair as a "momentous opportunity" and greeted warmly by the Bush White House. But the Blair government's moralizing about the IRA's "violent past" rings hollow in the face of the death and destruction caused by the U.S.-British invasion and occupation of Iraq--not to mention the much greater violence that Britain has used in Northern Ireland to maintain its rule over part of a divided Ireland.<p>
The IRA announcement generated a lot of attention in the media, but it came as little surprise to most people in Northern Ireland. The IRA and Sinn Fein--its political wing led by Gerry Adams and Martin McGuinness--have been publicly committed to ending the armed struggle for a decade and to using "constitutional measures" to advance their agenda, so the long-awaited declaration of disarmament will change little on the ground. <p>
By calling for a cease-fire in the 1990s, Sinn Fein, with the support of the IRA, entered a "peace process" whose terms were set out in so-called Good Friday Agreement of 1998. Since then, the British government and Loyalist supporters of British rule among Northern Ireland's Protestant majority have used the issue of IRA arms as a means of demanding more concessions and slowing down Sinn Fein's full participation in the political system. <p>
Pro-British Unionists led by Ian Paisley--who in the past refused any "power-sharing" arrangement with the Republicans of the Catholic minority, but who are now resigned to this inevitability--made an IRA "surrender" of weapons a central theme, in order to strengthen their hand in negotiations. The IRA's statement is meant to remove this barrier and allow the Republican movement to regain the political initiative.<p>
The abandonment of the armed struggle and the swing to electoralism reflects a contradiction in Sinn Fein's politics, according to socialist Eamonn McCann, a founder of the civil rights movement in Northern Ireland. <p>
"The contradiction is very stark and obvious," he told <i>Socialist Worker </i>in a recent interview. "Sinn Fein and the IRA are part of the same Republican movement, which claims to be a revolutionary movement fighting to remove the British presence from Northern Ireland. But in fact, they have been compromising with British rule for a very, very long time, and this reached its summit with the Good Friday Agreement."<p>
The modern IRA formed after 1969 in a response to bigotry and repression by Protestant Loyalists who ran the state machine in Northern Ireland. But what began as a legitimate defense against repression evolved into a clandestine guerrilla war carried out by an elite minority of fighters, with the working-class Catholic majority giving only passive support. Sinn Fein, meanwhile, engaged in conventional electoral politics, with the compromises and coalitions that this entails.<p>
The IRA's inability to militarily defeat British rule strengthened Sinn Fein's turn towards electoralism. The party abandoned its socialist rhetoric of the 1970s and embraced market-friendly economic policies. <p>
The Good Friday agreement, in which Sinn Fein accepted Northern Ireland as part of Britain, reflected this turn to the right. Thus, the official end of the IRA's armed struggle was the last link in a chain.<p>
The IRA's move won't mean the end of conflicts and crises in Northern Ireland. Reactionary Protestant leaders will doubtless try to provoke further armed clashes and blame the IRA. And some dissident IRA factions may continue to operate.<p>
But the agreement does signal a new phase in politics in Northern Ireland--one in which the struggle to end British rule will have to be linked to working-class economic and political demands.<p>


