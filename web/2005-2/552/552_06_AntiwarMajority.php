<font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Can there be progress without struggle?</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The new antiwar majority</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 5, 2005 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">WITH OPINION polls consistently showing a majority of Americans against the Iraq occupation, some prominent liberals are stepping forward to take credit for this welcome development. The "antiwar movement is winning by staying silent," was the theme of a recent column by <i>American Prospect </i>editor Harold Meyerson in the <i>New Hampshire Union Leader</i>.<p>
Congratulations are apparently in order to those responsible for the antiwar movement's hiatus throughout John Kerry's election campaign last year. "[H]owever perverse this may sound," Meyerson wrote, "the absence of an antiwar movement is proving to be a huge political problem for the Bush administration."<p>
Today's movement has cleverly avoided the mistakes made during Vietnam, according to Myerson, when a massive, militant movement helped Richard Nixon get re-elected by alienating the "silent majority." Today, he insists, the rising tide of antiwar opinion is a direct result of Democrats' <i>failure</i> to oppose the war.<p>
"With unprecedented discipline, Democrats who had opposed the war lined up behind the candidacy of John Kerry, whose position on the war was muddled at best," Meyerson enthused. Better still, he added, "the question of the occupation fell off the liberal agenda. At the Take Back America conference, a national gathering of liberals held [in June], the issue barely came up at all."<p>
Other antiwar liberals are offering an equally upbeat assessment--but in contrast to Meyerson, claim the movement's record of dedicated activism is responsible for shifting the political winds. On July 14, United for Peace and Justice (UFPJ), the nation's largest antiwar coalition, issued an action alert claiming, "Years of intense antiwar organizing are beginning to pay off in the legislative realm, with movement in both houses of Congress to call for U.S. withdrawal from Iraq."<p>
In reality, the antiwar movement suspended activist opposition to the Iraq occupation during Kerry's election campaign, as this would have proven awkward for the pro-war candidate. UFPJ's September 24 protest will mark its first national antiwar demonstration since the war began two-and-a-half years ago, other than the mobilization against the Republican National Convention.<p>
Americans' sentiment against the Iraq occupation is growing not so much because of the antiwar movement's achievements at home, but because the anti-occupation struggle in Iraq is succeeding in its aims.<p>
Nor is the picture on Capitol Hill quite as rosy as depicted by UFPJ. To be sure, some 60 members of the House of Representatives have formed an "Out of Iraq Caucus," urging "the return of U.S. service members to their families as soon as possible."<p>
But on July 20, by a margin of 291-137, the House approved a resolution stating "an early withdrawal" from Iraq would "embolden the terrorists," and the U.S. should leave only when its foreign policy goals have been achieved. The House also voted 304-124 for an amendment reiterating its support for the detention of terrorist suspects at Guant&aacute;namo Bay. Despite the antiwar majority in public opinion, Congress remains solidly behind the war.<p>
Antiwar activists now face a strategic choice: either galvanize the antiwar majority into a mass opposition movement or continue to prioritize lobbying Congress and supporting liberal (or not so liberal) Democrats.<p>
The antiwar movement cannot build an effective opposition to the war while it ties its fate to a pro-war party. <p>
Supporting Kerry last year resulted in an enormous setback for the antiwar movement. Those responsible for that failed strategy now seek to justify it in hindsight, using contradictory and unconvincing evidence, while taking credit where no credit is due.<p>
The torture at Abu Ghraib should have caused a crisis for the Bush administration last year, but the antiwar movement did not organize around the issue. The destruction of Falluja could have forced the massive Iraqi death toll into mainstream discourse, as the My Lai massacre did during Vietnam, but the movement remained silent despite the massacre of hundreds of Iraqi civilians. <p>
The movement's ability to affect the outcome of the war--including its ability to pressure Congress--lies in the strength of its numbers and the power of its protests. Without this, the notion that the antiwar movement is "winning" is an act of self-delusion.<p>

