<font face="Times New Roman, Times, serif" size="4">Bush signs CAFTA into law</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Central America's poor will suffer</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 5, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">AFTER PASSING the House of Representatives in a narrow 217-215 vote July 28, the Central America Free Trade Agreement (CAFTA) has gone to Bush for his signature.<p>
CAFTA will extend the Clinton administration's 1994 North American Trade Agreement to Costa Rica, El Salvador, Guatemala, Honduras, Nicaragua and the Dominican Republic.<p>
The bill squeaked through the House with the help of 15 Democrats who defected from their party's stated opposition to CAFTA, overcoming the 27 Republicans who voted against the bill.<p>
George W. Bush made last-minute appeals to House members, arguing that the trade deal was about national security and encouraging fledgling democracies. "We must not neglect the antidemocracy, anti-American forces that are at work in Latin America," chimed in Rep. David Dreier (R-Calif.).<p>
Afterward, a <i>New York Times </i>editorial cheered Democrats who crossed the aisle to vote for the deal. "Anyone who believes, as this page does, that the benefits of free trade outweigh those of protectionism should give a pat on the back to the Democrats who chose principle over politics and defied their party's leaders to vote for the trade pact," the <i>Times </i>wrote.<p>
Unions lobbied for a "no" vote on CAFTA. The sugar industry, for its part, also lobbied against it, challenging provisions that would cut subsidies to domestic growers.<p>
Largely, though, business interests--and their mouthpieces in the Bush administration--supported CAFTA because it strengthens the U.S.'s hand economically, and gives momentum to the broader Free Trade Area of the Americas (FTAA) initiative.<p>
In addition to eliminating barriers to trade in U.S. goods and investment, CAFTA also gives U.S. corporations the right to enforce patents and copyrights on CAFTA member countries. This means the pharmaceutical industry will have free rein to make super-profits because of protections against cheaper generic drugs--and the poor in Central America will pay the price.<p>
U.S. agribusiness is expected to make a killing when CAFTA goes into effect, as Central America is opened to U.S. imports.<p>
In the last hours before its passage, the Bush administration negotiated last-minute deals will lawmakers to secure their yes votes. For instance, Rep. Robert Aderholt (R-Ala.) told the <i>Los Angeles Times </i>that he was promised that if he voted "yes," the sock industry in his district would be protected, and he was presented with a letter signed by Commerce Secretary Carlos Gutierrez and U.S. Trade Representative Rob Portman to prove it.<p>
But opposition to CAFTA isn't so easily bought off in Central America, where the impending trade deal has sparked big protests, delaying ratification in Costa Rica, Nicaragua, the Dominican Republic and other countries.<p>
We have to build solidarity with our brothers and sisters in Central America--and show Washington that we won't sit down while their trade deals rob the world's poor.<p>


