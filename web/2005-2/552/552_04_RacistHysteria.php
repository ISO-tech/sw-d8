<font face="Times New Roman, Times, serif" size="5"><b>Fueling a racist hysteria</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 5, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>BRIAN JONES, a member of the International Socialist Organization in New York City, looks at the implications of de Menezes' murder in the U.S.</b> <p>

I WAS horrified (but not surprised) to read in the <i>New York Times</i> that London police are using Israeli tactics to "stop" suicide bombers. The article reports that a former London police commissioner sent teams to Israel to receive training--where they learned that "[t]here is only one sure way to stop a suicide bomber determined to fulfill his mission: destroy his brain instantly, utterly."<p>
Hence, the absence of any need to apologize for the seven bullets put into the brain of Jean Charles de Menezes, who, apparently running to catch a train on his way to work, was mistaken for a terrorist running from police.<p>
A correction is in order: For all their experience shooting Palestinians, the Israelis haven't actually stopped suicide bombers. Surely the British know this. After generations of dealing with Irish Republican Army attacks, have the London police seriously concluded the solution to terrorism is a question of marksmanship? I think not.<p>
Now that New York City subway riders are subject to random searches, one gets the feeling that the point is not at all to "stop" terrorists, but to stop us from forgetting about terrorists. <p>
The point is to obscure the real cause of the conflict: the war in Iraq. We are supposed to stop worrying about why U.S. troops are still in Iraq and start worrying about the person with brown skin next to us on the subway.<p>
It's not marksmanship the Israelis are teaching, but social control. Bush and Blair have learned from Prime Minister Ariel Sharon that a population that thinks it's under constant threat of attack tends to support perpetual warfare. Instead of the antiwar movement slogan "we are all Palestinians," the subway searches send the message that "we are all Israelis." This is the real meaning of the death of Jean Charles de Menezes.<p>
Tragically, this plan is being set in motion at the precise moment that the American antiwar movement is splitting over the question of Palestine. The conservative wing of the movement, led by the United for Peace and Justice coalition, is refusing--in the name of being "inclusive"--to come out openly in support of the Palestinian struggle. The price of "inclusion" for liberals and Democratic Party politicians is the exclusion of Arabs and Muslims (the very people the Israelis are teaching us to shoot in the head).<p>
How will the movement deal with further suicide bombings in London and, possibly, New York? If the movement is silent on Sharon's manipulation of tragedy, how can it be loud when Bush does it? If Bush is wrong, how can Sharon be right? Aren't they both just trying to "protect us" from the terrorists?<p>
When Martin Luther King broke with the Democratic Party to come out against the Vietnam War, he said, "A time comes when silence is betrayal." Israel's offensive war on the Palestinians has always hidden behind language about "fighting terrorism." Now that Bush and Blair are explicitly working from Sharon's playbook, continued silence about Palestine has become betrayal.<p>

