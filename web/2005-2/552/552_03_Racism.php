<font face="Times New Roman, Times, serif" size="5"><b>The grip of racism in Bush's America</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 5, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">TWO YEARS ago, the city of Benton Harbor in southwestern Michigan captured rare headlines when hundreds of Black residents rose up in rebellion for several nights, driving police off the streets and setting fire to abandoned buildings.<p>
The rioting was a response to the latest brutality committed by the area's overwhelmingly white police--the death of a Black motorcyclist after a high-speed chase. But--as the mainstream media briefly noted before forgetting about Benton Harbor--the other ingredient was a long history of economic neglect that left unemployment at over 40 percent and one-third of the city's households with an annual income of less than $10,000.<p>
Two years later, Benton Harbor is reacting to another racist outrage--but only the progressive media are paying attention.<p>
Community leader Rev. Edward Pinckney helped to organize the successful campaign to recall a city commissioner, Glen Yarbrough, who is notorious for supporting the police--and doing the bidding of corporate giant Whirlpool, which is currently scheming to knock down a building for seniors to build upscale housing and a golf course. Now Pinckney has been charged with buying votes in the recall election. Even if he beats the trumped-up charge, the legal costs could bankrupt him.<p>
Forty years after the passage of the Voting Rights Act that formally ended the era of Jim Crow disenfranchisement, Benton Harbor shows the reality of racism in America that is rarely--if ever--acknowledged by politicians or the media.<p>
The real sources of the crisis of Black America are easy to find. Median household income for African Americans fell by 3 percent in 2002 and more than 6 percent between 2000 and 2003. Black unemployment is 10.1 percent, nearly twice the national jobless rate--and joblessness among African American youth is several times that.<p>
During the 1980s and 1990s, government spending on the prison system grew at six times the rate of spending on higher education. The consequences for Blacks have been traumatic--African American men make up only 6 percent of the U.S. population, but are half of all male prisoners in local, state and federal jails.<p>
Statistics like these show the crying need for the Millions More Movement demonstration, set for October 14-16 in Washington, D.C. Called by the Nation of Islam's Louis Farrakhan and Revs. Jesse Jackson and Al Sharpton, the protest will take place on the 10th anniversary of the Million Man March. Ten years later, reads the protest Web site, "the masses of our people are slipping further behind."<p>
In contrast to the Million Man March's emphasis on "personal atonement," this year's demonstration centers on calls for an end to police brutality, racial profiling and the herding of young Black men into prison; decent health care and education; and opposition to economic attacks that have led to declining living standards. <p>
The opportunity to come together for a protest that highlights racism, poverty and the attack on working-class living standards is welcome.<p>
Unfortunately, one of the Millions More Movement national organizers, Rev. Willie Wilson, recently spewed anti-gay bigotry in a sermon at his Washington, D.C., church. Former D.C. Mayor Marion Barry, who is backing the October demonstrations, met with angered gay and lesbian activists, and is calling on Wilson to apologize. But the silence has been deafening from the main march leaders--including Sharpton, one of the only candidates for last year's Democratic presidential nomination to support equal marriage rights.<p>
Anti-gay bigotry has no place in a demonstration for civil rights. It can only set back this important opportunity to take a stand against racism, poverty and injustice.<p>

