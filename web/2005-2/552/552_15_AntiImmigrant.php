<font face="Times New Roman, Times, serif" size="5"><b>Anti-immigrant racism is "an injury to us all"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By William Figueroa and Freddy Garcia</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 5, 2005 | Page 15</font> <p>

<font face="Times New Roman, Times, serif" size="3">LAGUNA BEACH, Calif.--The racist group Save Our State (SOS) called a demonstration to intimidate immigrants at a city-funded day laborer center here.<p>
This was the latest attempt by SOS to recruit to their efforts to harass immigrants, particularly undocumented Latino workers. Even as they complain that their group has been labeled "racist," they have embraced the presence of neo-Nazi groups like Stormfront and the National Alliance at their events. <p>
What awaited them this particular morning was a militant antiracist counter-protest of more than 100 day laborers and activists that shouted down the 50 bigots and Nazi sympathizers. Armed with placards, banners and chants, pro-immigrant groups loudly broadcast the message that "Immigrants are welcome here" and "No human being is illegal."<p>
In the summer sun, our side forced most of the skinheads with white power tattoos to retreat across the main road. As they huddled across the way, we pushed forward, determined not to let the Nazis display their flags anywhere near the day laborer center. <p>
A group of 60 antiracists and day laborers headed down the road towards the bigots, and we held our ground until they left. The police were then compelled to make them leave, even arresting one of their members for trying to provoke our side, and the few remaining SOS members decided to tuck tail and leave as well, scoring a victory for our side. <p>
"It is encouraging to see that there are not only Latinos defending us, but also white, Black and Asian people here supporting us," said Carlos, one of the day laborers. "No longer should we be ashamed to ask for help in our fight as day laborers." <p>

-- In <b>Las Cruces, N.M., </b>hundreds of protesters marched through the streets here July 23, chanting "Hey, hey, ho, ho, Minutemen have got to go!" The march was called by the League of United Latin American Citizens (LULAC) to protest the anti-immigrant Minutemen group coming to New Mexico.<p>
The Minutemen are at the forefront of a new and dangerous surge in anti-immigrant activism in the U.S. Two weeks ago, the Minutemen organized their first meeting in New Mexico, announcing plans to descend on the border this fall.  <p>
LULAC and its allies jumped into action and called this rally, which was attended by hundreds of immigrant rights' supporters from Los Angeles to Texas. "When they hurt one of us, they hurt all of us," Garc&iacute;a, a Los Angeles LULAC member, told <i>Socialist Worker.</i> "We had to be here."<p>
<i>Jeff Bale contributed to this report</i>.<p>

