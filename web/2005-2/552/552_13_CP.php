<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The CP: Liberals in radical clothing</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 5, 2005 | Page 13</font><p>

<font face="Times New Roman, Times, serif" size="3">LIBERO DELLA Piana (see this issue's "View and Voices" page) takes issue with the fact that my last column ("The CP adapts to the right...again") makes the case that the Communist Party USA recently reaffirmed its support for the Democratic Party.<p>
In addition, he points out my failure to note that the CP repudiated "Browder, Stalin and the internment of Japanese Americans."<p>
The CP expelled Earl Browder in 1944 not as a repudiation of the "democratic front," but because Browder became so carried away with obeisance to the Democrats that he dissolved the party.<p>
As for repudiating Stalin--naturally, after Kruschev's revelations of Stalin's crimes in 1956, most CPs distanced themselves from Stalin, though they retained support for the USSR, identifying its bureaucratic, one-party state as an expression of socialism rather than an obstacle. The CP supported the Soviet Union's suppression of the Hungarian workers' uprising in 1956 and the invasion of Czechoslovakia in 1968.<p>
During the suppression of the Polish workers movement Solidarnosc in 1981, Gus Hall, party leader until 1999, wrote: "martial law was a necessary emergency step in order to prevent the anti-socialist counter-revolutionary forces from taking power."<p>
In response to my "charge" that the CP reaffirmed its support for the Democratic Party at its recent convention, Libero in fact confirms it, writing that the CP has "recommitted" itself to "build an all-people's alliance against the ultra-right." But this phrase, when translated into proper English, means: Anybody but Bush. It's a position the CP openly adheres to, so I'm not sure what Libero is objecting to.<p>
The quote from Lenin, about the need to take advantage of splits within the bourgeoisie, is simply an attempt to provide a left-sounding cover for a liberal position. Libero neglected to add that for Lenin, taking advantage of splits at the top did not involve <i>political support for any faction of the bourgeoisie</i>.<p>
And though the Bolshevik Party made all sorts of practical alliances, they never for a moment relinquished their political or organizational independence, or refrained from criticizing their allies whenever they wavered.<p>
Support for the Democrats means precisely muting criticism for the sake of unity, and <i>covering up</i> the Democrat's real nature. How else can you sell a "people's front" with a bourgeois party that supports military adventures abroad, free-market globalization, and attacks on wages and social spending at home?<p>
Here is a post-election assessment by Norman Markowitz, a contributing editor of the CP's <i>Political Affairs: </i>"Denouncing the Democrats, blaming the Kerry campaign, is not the way to go. Kerry ran a decent campaign by Democratic Party standards...The left can only become stronger by actively fighting the right, not by attacking the Center. And the left can only become stronger in alliance with the center (meaning those called liberals in the U.S.)."<p>
CP National Chair Sam Webb, during the run-up to the 2004 election, wrote, "The remark heard in some left circles, 'I will vote for Kerry but hold my nose,' is counterproductive and demobilizing. It...will do little to convince swing, undecided, or stay-at-home voters to go to the polls."<p>
"The responsibility of left and progressive people," concludes Webb, "is not to spend their time bellyaching over Kerry's shortcomings, but to convince millions that there is a choice and that the outcome of this election will have enormous consequences for our nation's future."<p>
The logic of popular frontism, on display in the two quotes, is to accommodate to liberals in order not to frighten them, and to provide cover for them when they behave like...liberals.<p>
Hence, though the <i>Political Affairs</i> article calling the fight for gay marriage "too much, too soon" was only a guest opinion, it is telling that it was posted without a strong response carrying the position that Libero claims the CP holds.<p>
I look forward to any further debate of these issues--but there is little point in trying to hide the CP's support for the Democrats in pseudo-radical language.<p>

