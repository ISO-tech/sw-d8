<font face="Times New Roman, Times, serif" size="4">Why did EPIC shut down the endthewar.org site?</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The waste of a resource</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 5, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">THE EDUCATION for Peace in Iraq Center (EPIC) has taken down the Endthewar.org Web site and redirected all of Endthewar.org's visitors to EPIC's Web domain. <p>
Endthewar.org was the Web site name of the National Network to End the War Against Iraq (NNEWAI). Reportedly, EPIC "owned" the domain name for Endthewar.org and recently asked that the Web site return to EPIC's control.<p>
A substantial historical record of the grassroots antiwar movement has been made inaccessible (though material is retrievable temporarily through Google's caches). Over 350 grassroots organizations belonged to the national network. The Endthewar.org site contained a database of contact information for all of these groups--names, addresses, e-mails and Web sites. All of this is no longer available through Endthewar.org. <p>
Traprock Peace Center was given one day's notice that the site was being shut down. We objected to the Web manager and scrambled to save what we could of the site. We were able to save the membership database. This is an invaluable resource for horizontal networking of grassroots organizations. (You may access the saved index at <a href="http://www.traprockpeace.org/endthewar_member_index.html" target="_blank">http://www.traprockpeace.org/endthewar_member_index.html</a>)<p>
On the page that EPIC erected to explain what has happened, EPIC gives itself much credit for NNEWAI's formation. We do not dispute EPIC's account of NNEWAI's formation, as we have no personal knowledge of those events. <p>
Whatever role EPIC had at one time, however, NNEWAI became an independent national grassroots network of local organizations. It was not controlled by EPIC, at least as of 2003. Traprock, in fact, became the fiscal sponsor for the NNEWAI during 2003 and for 2004, taking over from one of the grassroots organizations. Traprock took on this role as a service at the request of NNEWAI's national coordinating committee. <p>
Traprock did not control the work of the Network but did participate in some national initiatives. For example, Traprock initiated the February 15, 2004, international candlelight vigil for peace, which the Network sponsored as an international project.<p>
While it is true that the Network did not coordinate national actions or events during the latter part of 2004 and into 2005, I am informed by a national coordinator that the Coordinating Committee did not disband the Network. Neither did the Network give formal approval to a name change, as indicated by EPIC. And, while its Web manager stopped updating the Web site in October 2004, the site remained an invaluable resource. <p>
EPIC itself acknowledges that Endthewar.org had--and I am quoting EPIC--"incredible resources." This being the case, how could EPIC justify taking the important Web site and replacing its "incredible resources" with a single page that "explains" what happened and ends with a pitch to join EPIC? <p>
Many Web sites linked to Endthewar.org, and no doubt hundreds of people a day go to Endthewar.org looking for its resources. Activists, historians, writers and the public have suffered a tremendous loss. It is an easy matter to host a Web site's content if you already host a Web site. There is no good reason to keep this important material offline. Traprock is willing to host the entire site if it becomes available.<p>
EPIC recently has come under fire within the antiwar movement for supporting the continued occupation of Iraq by U.S. forces. EPIC says its position is to bring a "responsible end" to the occupation. Many activists and organizations--including Traprock--consider this position to amount to its support for continued occupation. <p>
Traprock covered an important debate on this topic sponsored by Brooklyn Parents for Peace. Anthony Arnove (noted author and member of editorial board of the <i>International Socialist Review</i>)and Alex Ryabov (a founding member of Iraq Veterans Against the War) challenged EPIC's position, as represented by Eric Gustafson. EPIC criticized Traprock for its characterization of the debate. Judge for yourself (You may hear the debate, download the transcript and see photos at <a href="http://www.traprockpeace.org/iraq_debate_06april05.html" target="_blank">http://www.traprockpeace.org/iraq_debate_06april05.html</a>.).<br>
<b>Charlie Jenks, </b>Traprock Peace Center, Deerfield, Mass.<p>

