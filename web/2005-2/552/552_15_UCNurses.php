<font face="Times New Roman, Times, serif" size="4">Governator and University of California retaliate against CNA</font> <br>
<font face="Times New Roman, Times, serif" size="5"><b>UC blocks nurses' strike</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jessie Muldoon and Andy Libson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 5, 2005 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">NURSES AT the University of California (UC) system called a one-day strike for July 21 at campuses around the state, but Gov. Arnold Schwarzenegger's appointees to the Public Employees Relations Board (PERB) blocked the strike the day before it was to take place.  <p>
For months, the California Nurses Association (CNA) has been battling with Schwarzenegger over patient care and nurse-to-patient ratios, challenging him at all fundraisers and public appearances he has been making. Schwarzenegger has targeted nurses as well as teachers and other public servants as "special interests" and has made attacking the unions a centerpiece of his effort to balance the state budget.  <p>
For their part, health care unions like CNA have spearheaded the precipitous drop in Schwarzenegger's popularity--and have forced him to reconsider the special California election proposed for November 8.<p>
So when Schwarzenegger's PERB carried out what appears to be a retaliatory swipe at the union, nurses were infuriated.<p>
"Efforts by those linked to the Schwarzenegger administration to silence the voice of UC nurses follow months of attacks by Schwarzenegger on RNs," said CNA Executive Director Rose Ann DeMoro in a statement. "This governor has repeatedly tried to roll back the law requiring safe nurse hospital staffing and now faces contempt of court charges for defying a court order, proposed cutting retirement benefits for nurses and other public servants, tried to abolish the independent nursing board, and vetoed a bill to reduce workplace injuries for nurses."<p>
While it is true that Schwarzenegger is targeting the nurses, the major culprit is the UC Board of Regents, which wants to enlist Schwarzenegger's aid in extracting major concessions from the nurses by limiting work actions.<p>
In place of the strike, the CNA organized pickets and rallies at UC Medical Centers and hospitals. At the Tang Center at UC-Berkeley, about 60 people picketed and sang labor songs. Representatives from the Union of Professional and Technical Employees, the Coalition of Union Employees and AFSCME showed up to lend their solidarity. <p>
A major CNA concern is UC's attack on their pension plans, which would require an 8 percent employee contribution--essentially a pay cut--for a plan that provides less protection than under previous contracts. "We have been taking cuts in pay for years in order to keep a secure pension plan, and now they want to start cutting into that," said Irene Cale, a nurse at UC-San Francisco.  "I don't think I can work in the UC system if this goes through."  <p>
Janis Fox-Davis, a nurse with 26 years of experience who was set to retire the day after the blocked strike, bluntly described the change in conditions for nurses during her career. "Nursing used to be about being in a caring profession. Now, it is a work camp."<p>

