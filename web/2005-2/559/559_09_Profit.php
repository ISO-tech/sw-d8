<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>How the bosses make their profits</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 30, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">A KEY axiom of Marxist economics is that real wealth under the capitalist mode of production is mostly created by labor. This excludes, of course, useful things that are still (though less and less so) freely available without the intervention of human labor (air, for example).<p>
Capitalist wealth presents itself to us as, to quote Marx, "an immense accumulation of commodities." And commodities are the product of labor.<p>
When looking at the aggregate level of the system as a whole, surplus value--the source of profit--consists of the excess of labor performed over and above the cost of labor power (wages). In short, profit is unpaid labor, or the value added to commodities by workers over and above what it costs to set them to work.<p>
Capitalists can increase their profits in several ways: by cutting labor costs (wages and benefits), increasing the intensity of labor (making people work harder), or introducing labor-saving machinery that reduces the amount of labor per unit of production.<p>
All other means of increasing profits involve siphoning off surplus value already produced by unpaid labor elsewhere--in other words, not the increase of wealth but its redistribution between various capitalists as they scramble to outperform each other.<p>
Monopoly pricing or price collusion is a good example. If I control a certain market, I can overcharge.  In this case, the extra profit I've made doesn't come from any surplus value produced by "my" workers but a siphoning of wealth produced elsewhere by other workers.<p>
This is certainly what happens with the pharmaceutical industry, where profit rates are routinely higher than other sectors by imposing, with government support, inflated drug prices.<p>
Costs can be lowered in a lot of different ways, sometimes more indirect than simply cutting wages. Robert Miller, former chief executive of the now-defunct Bethlehem Steel, is a case in point.<p>
The <i>New York Times</i> recently referred to him as "a turnaround artist with a Dickensian twist. He unlocks hidden value in floundering Rust Belt companies by jettisoning their pension plans. His approach, copied by executives at airlines and other troubled companies, can make the people who rely on him very rich."<p>
This "artist" buys ailing companies on the cheap, then uses bankruptcy proceedings to dump their pension funds on the federal government. In essence, he forces other workers (as taxpayers) to pay for whatever (usually drastically cut back) pension obligations remain.<p>
Having dumped enormous costs and raised the bottom line, the companies' stock values revive, and he then sells them for far more than he paid for them. According to the <i>Times</i>, "bankruptcy specialists say that it is almost certain to keep happening, because shedding pensions--and pensioners' health care obligations--is turning into an irresistible way to make a high-risk investment pay off."<p>
The <i>New York Times</i> later ran a story about a guest workers' protest in Dubai. Apparently, workers there often don't get paid for months on end, and often get shortchanged or don't get paid at all.<p>
Anyone can see that this is an outrage--in Dubai. But in the U.S., stealing workers' pensions and getting the state to underwrite a company's profitability is considered good business. And from a capitalist perspective, this is absolutely true. Any opportunity to lower wages--including national disasters--is an opportunity no capitalist will pass up. <p>
Bush, the quintessential capitalist politician, was well aware of this when he announced that contractors rebuilding on the Gulf Coast could pay less than prevailing wages to their workers. And businessmen in towns around Louisiana and Texas will benefit not only from government reconstruction money that will disproportionately help them over the poor, but from the higher number of workers seeking jobs in the region in the wake of Katrina and Rita that will allow them to pay lower wages.<p>
We are meant to get upset about poor people looting stores in New Orleans, but the entire edifice of capitalism is founded upon the systematic looting of the working class.<p>

