<font face="Times New Roman, Times, serif" size="5"><b>Arbitrator  panel attacks NYC teachers</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Peter Lamphere, United Federation of Teachers</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 30, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--The attacks on public education here were escalated last week with the release of a state arbitration panel's report on the contract negotiations with the teachers' union.<p>
Education workers in the United Federation of Teachers (UFT) have been locked in a battle with billionaire Mayor Mike Bloomberg and without a contract for more than two-and-a-half years.<p>
In their report, mediators demanded a string of unprecedented concessions from the 100,000-member union, including an extra 10 minutes added to the school day (on top of 20 minutes conceded in the previous contract) and three working days added to the calendar. When the time-for-money trades are taken into account, the report's offer of an 11 percent raise boils down to a mere 4 percent--which is far below the rate of inflation during the past three years.<p>
But the concessions demanded in terms of work rules are even worse than the miserable pay proposal. The right to grieve disciplinary letters would be removed, and the potential for lunchroom duty (eliminated years ago by the union) reinstated. <p>
In an effort to split high school and middle school teachers from elementary educators, an extra 10 free substitution periods are being demanded from teachers in the higher grades.  Of course, there is massive anger among teachers about the fact-finders' report, which found expression in an explosion of criticism and debate on the union's own blog. <p>
Sadly, the union leadership's response has been to accept the non-binding report as a "vehicle" and "framework" for future negotiations. This means that, although cosmetic modifications may be made at the bargaining table, the basic givebacks in the fact-finders' report will probably be the end result of any negotiations. <p>
When the UFT entered the arbitration process almost a year ago, many rank-and-file activists warned that unless the union actively prepared by grassroots mobilizing, we could be caught without any leverage when the report came out.<p>
And that is exactly what happened.  Although the union has threatened to take a strike authorization vote in three weeks if the mayor has not sat down at the table (or consider endorsing Democratic candidate Fernando Ferrer in the upcoming election), it is clear the union is unprepared for serious strike action.<p>
An "action plan" last spring, which launched a number of productive in-school actions, culminated in a pep-rally at Madison Square garden--with a few famous musicians and visits by politicians. In fact, the leadership is much better at threatening members with the penalties of striking under New York's draconian anti-union Taylor Law than it is at mobilizing members for the eventuality of using their collective power.<p>
However, nearly a quarter of delegates voted to discard the leadership's plan to use the arbitrator's report as a "vehicle," which is the start of a potential campaign to vote down a concessionary contract. <p>
The anger of the membership at any contract that incorporates extra time was expressed in a near-rebellion in the union last fall over a potential deal with the mayor that would have exchanged 20 extra minutes for a 14 percent raise. But will any of the anger that educators feel across the city be turned into action at the school level?  <p>
A serious campaign of picketing and rallies is needed that could coalesce the confidence of members and begin the job of educating members about a potential strike. To effectively challenge the mayor, however, these actions would have to escalate in order to push the limits of the Taylor law, such as rotating sickouts or coming in 10 minutes late en-masse.<p>
However, in the face of a concessionary contract, a "vote no" campaign led by the rank and file is the only way to force the leadership into action--and deliver a blow to a system that continually devalues education and de-prioritizes teachers' salaries and working conditions.<p>
