<font face="Times New Roman, Times, serif" size="4">George Galloway:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"The political landscape is shifting"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 30, 2005 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">GEORGE GALLOWAY is a British antiwar leader and newly elected member of parliament for the Respect Coalition. He spoke at the September 24 demonstration in Washington, D.C., following a successful antiwar speaking tour across North America that brought out more than 10,000 people. He talked to <i>Socialist Worker </i>about his impressions of the U.S. following his tour.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">I HAVE a very clear impression that the landscape is shifting in the wake of the Cindy Sheehan phenomenon, and in the wake of the disaster of Hurricane Katrina and its exposure of the pitiful inadequacies of the Bush administration and the political system to deal with the issues of class and race and deprivation that were thrown up in that experience.<p>
I know that the landscape is shifting on the other side of the Atlantic very fast also. In the wake of the events in Basra over the last few days, the demand for the immediate withdrawal of British forces is now fully 86 percent in some of the polls. Which is effectively--give or take the statistical margin of error--everybody in the country except Tony Blair and his family. This is a remarkable shift.<p>
We were always right about the war. We were vindicated very quickly with the exposure--layer by layer, like an onion being peeled--of all the lies that led to it. But now, despite the fog of war propaganda and disinformation, somehow, the people have found their way to the truth and are reaching the right conclusion. <p>
I'd like to claim that the antiwar movement had something to do with that. But the good sense of the people of both our countries is even more significant in that regard.<p>
In Britain, our position is slightly different to yours. Our 8,000 soldiers are now exposed in a sea of 10 million Shiite Muslims in the south of Iraq, who seem to have concluded that it's time for them to go. That is potentially a catastrophe for the British political class, as well as the 8,000 soldiers and their families. A bloodbath of unimaginable proportions could well overwhelm the British forces who I've described as standing just one fatwa away from such a disaster. <p>
So the urgency of the British demand for withdrawal--shared, as I say, by the vast, overwhelming majority of the people--is going to be impacting the British political scene. I believe when parliament goes back at the end of this month, you'll start to see some significant shifts in Britain.<p>
In the United States, I think you have to do more of the same. You have to accelerate your work with the families of the service people--not just those who've died and those who've been wounded, but those who are being scarred by the experience of serving in this ignoble war and who will, after the Vietnam example, come back to their own country with their lives gravely affected by what they've seen and done.<p>
I think you also need to find greater unity. That's been, I think, a problem in the U.S., one we really haven't had in Britain, where all the significant forces are under the banner of the Stop the War coalition. I'm the vice president of it; I know how successful that has been therefore.<p>
The Muslim community in America needs to be more fully engaged. The left elements who have some inherent suspicion or unwillingness toward dealing with Muslims have to get over that, because we will be much stronger if the Muslims of America are with you, and they will be much stronger, too.<p>
In a desert, it only takes a few drops of rain to transform the landscape. I think the absolute absence of the Democratic Party from this great shift that's unfolding in this country has left a desert. I think that the plants in this desert are just waiting for those few drops of rain. We have to provide them with that. With the tour that we did, and with Cindy Sheehan much more so, we are helping to transform the landscape.<p>
 he elephant--or should I say donkey--sitting in the corner of this debate is that the Democrats, who could be exploiting this great wave of anger against Bush and the neocons and all their works, can't do so because they are hopelessly, and perhaps fatally, compromised by their own embrace of these very same policies. <p>
There must be many decent people still in the Democratic Party who lament that. I would urge that they not put party before country, do not put party before movement, but that they throw themselves wholeheartedly into this movement.<p>

