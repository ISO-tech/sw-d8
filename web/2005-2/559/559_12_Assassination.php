<font face="Times New Roman, Times, serif" size="4">Leader of Puerto Rico's nationalists shot down</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Murdered by the FBI</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 30, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">FBI SHARPSHOOTERS killed a leader of the Puerto Rican independence movement September 23 in an operation that bears all the hallmarks of a political assassination.<p>
Filiberto Ojeda R&iacute;os, the 72-year-old leader of the Machatero group, bled to death after being shot during an FBI siege of his farmhouse. An autopsy showed that he could have survived had he received prompt medical attention--but FBI agents refused to allow access to the house for 24 hours after he was shot.<p>
The FBI claimed that they only fired after shots came from the house. But Ojeda R&iacute;os' wife, Elma Beatriz Rosado Barbosa, told reporters that the FBI stormed the property at 3 p.m. and began firing powerful weapons into the front wall of the house. She was allowed to leave, and was tackled and searched.<p>
But the FBI refused Ojeda R&iacute;os' offer to surrender in the presence of a prominent Puerto Rican journalist--and let him bleed to death on the anniversary of an 1868 uprising against Spanish colonial rule.<p>
This was an unmistakable message from the U.S. government, colonial masters of Puerto Rico since 1898. "Only a Yankee-lover of the worst kind could doubt that the intention of the FBI in murdering to Filiberto Ojeda R&iacute;os on the 137th anniversary of the Grito de Lares [insurrection] was to trample the historic legacy of the fight for the independence of our nation," stated an editorial the New York-based Puerto Rican newspaper <i>El Diaro</i>.<p>
Outrage swept the island as news of Ojeda R&iacute;os' killing spread. Students marched through the University of Puerto Rico September 26, removing the U.S. flag and lowering the Puerto Rican flag to half-mast. They forced fast-food restaurants like Burger King to close out of respect for Ojeda Rios, and a panicky university administration gave students and workers the afternoon off.<p>
Even mainstream politicians voiced outrage, claiming that they didn't know about the FBI's plans in advance.<p>
"The politicians are being hypocritical," said Roberto Barretto, a member of the Organizaci&oacute;n Socialista Internacional (OSI) in Puerto Rico. "The assassination shows the basic relation with the U.S.--that Puerto Rico is a colony--and the government is trying to save face."<p>
Ojeda R&iacute;os had been a fugitive since 1990, when he removed an electronic bracelet while under house arrest in connection with a 1983 bank robbery in Connecticut, allegedly carried out by the Machetero pro-independence group, which has been mostly inactive for a decade. Symbolically, Ojeda R&iacute;os chose to remove the bracelet on September 23, and each year on that date, he has released audio tapes with messages to the pro-independence movement.<p>
The U.S. colonial authorities have chosen Puerto Rican nationalist holidays to make their own bloody statements as well. On July 25, 1978--the 80th anniversary of the U.S. invasion of the island--the FBI and Puerto Rican police assassinated two young Puerto Rican independence activists in Cerro Maravilla after an undercover cop lured them into joining a plan to blow up a television station.<p>
Today, according to OSI's Barretto, the authorities are trying to intimidate pro-independence activists, the broader left and the social movements, which together succeeded in driving the U.S. Navy off of the island of Vieques in 2003. "The assassination of Ojeda R&iacute;os might be as big as Cerro Maravilla in terms of political repercussions," he said. "The U.S. wants to link this to the war on terror, and respond to the revolts in Latin America. They don't want to see another Vieques."<p>

