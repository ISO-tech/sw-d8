<font face="Times New Roman, Times, serif" size="4">State Department ignores human rights abuses</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Dirty war in Colombia</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 30, 2005 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">IN AUGUST, the U.S. State Department certified that Colombia's government has made progress in the area of human rights. The certification cleared the way for more military aid to the country--despite continued reports of human rights abuses and government ties to paramilitaries and drug traffickers.<p>
Currently, the government of President Alvaro Uribe is attempting to reintroduce paramilitaries into Colombian society under a measure called the Justice and Peace Law. This follows a U.S. Justice Department opinion (yet to be made public) drafted in June that would allow the U.S. to help Colombia to fund the "disbanding" of brutal far-right paramilitary groups--despite a U.S. ban on material support to groups on the State Department's terrorism list.<p>
Activists say that money for demobilizing paramilitary groups would likely be redirected back into their operations. As Jos&eacute; Miguel Vivanco, Americas director for Human Rights Watch, recently commented, "The government's failure to conduct the demobilizations in a serious manner is helping paramilitary commanders launder their wealth and legitimize their political power. "Having interviewed numerous demobilized paramilitaries, government officials, and other insiders, it is evident this process is rotten to the core."<p>
Here, we reprint a letter from Colombia Support Network President JOHN LAUN to Rep. Thomas Petri (R-Wis.), explaining why U.S. funding of Colombia's dirty war must end.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">I WAS very disappointed to hear that the State Department on August 2, 2005, certified that the Colombian government has made progress on human rights and released military aid which had been withheld from the Colombian government both for 2004 and for 2005. <p>
These funds should not have been released (certification should have been denied) for the following reasons:<p>
1. The administration of President Alvaro Uribe Velez has implemented a program for "reinsertion" of illegal paramilitary forces into Colombian society which provides for virtual impunity for their crimes--massacres of hundreds of law-abiding noncombatants and forcing hundreds of thousands of Colombian peasants, indigenous and Afro-Colombians off their lands--while placing them in positions and paying them as policemen and other local authorities, where they will be able to continue their murder and pillage.<p>
2. The paramilitaries whom President Uribe is favoring are Colombia's principal drug-traffickers. The decision to release funds to Colombia is a decision in favor of promoting the continued traffic of cocaine from Colombia to the cities and towns of the United States.<p>
3. President Uribe has long had close ties to some of Colombia's biggest drug-traffickers, as the U.S. CIA has documented. His family almost surely owes its wealth to drug-trafficking. And President Uribe himself celebrated last Christmas with a convicted drug-trafficker who had returned to Colombia after serving a prison sentence for his crimes in the United States. It is intolerable that the U.S. government overlooks these ties and the corruption of the Uribe government!<p>
4. The Colombian Army has recently been responsible for several very serious human rights violations, including the murder on February 21, 2005 of eight members of the Peace Community of San Jose de Apartado. Instead of progress on human rights, the Colombian military has shown just the reverse. The State Department's decision to certify will only serve to promote criminal conduct by the Colombian military, in close cooperation with the paramilitaries the Uribe administration is favoring.<p>
I urge you to deny funds to the Colombian military and to the Uribe government for its fatally flawed "reinsertion" or demobilization program for paramilitaries. Your vote for continued funding for Plan Colombia, now called Plan Patriota by the Colombian government, sends U.S. taxpayers&#8218; funds to a government that promotes human rights abuses through its army and welcomes drug-traffickers at the highest levels of its government. <p>
It is high time to end U.S. support for the corrupt Uribe regime. It's not only Colombia's problem; it's ours as well.<br>
<b>John I. Laun, </b>President, Colombia Support Network<p>
