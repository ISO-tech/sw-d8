<font face="Times New Roman, Times, serif" size="5"><b>A "counter history" in hip-hop music</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Bob Quellos</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 30, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Jeff Chang, <b><i>Can't Stop Won't Stop: A History of the Hip-Hop Generation.</b></i> St. Martin's Press, 2005, 560 pages, $27.95.</font><p>

<font face="Times New Roman, Times, serif" size="3">WHEN KANYE West told us a few weeks back that, "George Bush doesn't care about black people," we were quickly reminded that hip-hop can be political as much as it can make us dance.<p>
"That's what it's supposed to do--speak truth to power," Jeff Chang, the author of <i>Can't Stop Won't Stop: A History of the Hip-Hop Generation, </i>told <i>Socialist Worker.</i> "I'm not surprised by this...hip-hop tells the micro history, but it also tells the counter-history to the official narrative."<p>
This counter-history is exactly what Chang--hip-hop's Mike Davis--has delivered in his first book, <i>Can't Stop Won't Stop.<p>
</i>Through the telling of the history of hip-hop, Chang helps fill a void in political discussion that's too often forgotten--the social-economic devastation that wrecked American cities and the working class during the 1970s. Even more importantly, the book links the conditions in the 1970s with the political climate today.<p>
Chang begins at the 1977 World Series at Yankee Stadium. Inside the ballpark, Reggie Jackson was taking on Yankee manager Billy Martin, who often referred to Jackson as "boy." Outside the park, an abandoned school was on fire, leading sports announcer Howard Cosell to claim on live television that "the Bronx is burning."<p>
Fires in the Bronx weren't rare. Between 1973 and 1977, more than 30,000 arson fires were set in the South Bronx. As Chang states in the book, "arson was highest form of capitalism" for landlords in an area that had already lost 40 percent of its manufacturing base and was suffering 60 to 80 percent unemployment among its youth population.<p>
Jobs were not the only thing that had left the Bronx--by 1968, the radical movements were starting to disappear. The South Bronx saw the political vacuum filled by youth gangs who eventually were seen by many residents as "the real law on the street" as they aimed to rid neighborhoods of drug dealers, addicts and the NYPD.<p>
Within a few years, violence between rival gangs began to peak. Two choices lay ahead--peace or more blood. Through a gang treaty, peace prevailed in the end that opened up the boroughs, allowing for an exchange of music and ideas that was impossible earlier. <p>
"The gang peace treaty of New York in 1971 is probably one of the most important modern histories of NYC, but you never hear about it," Chang told <i>Socialist Worker. </i>"The effects of it are still with us around the world, that effect being hip-hop."<p>
As the gangs diminished, a new generation emerged that was "obsessed with flash, style, sabor [flavor]," led by DJs Africa Bambaataa's Zulu Nation and Kool Herc and the Sound System. This laid the groundwork for hip-hop's emergence from the block party to an international movement.<p>
Chang traces this movement from the early 1970s dance parties in Kingston, Jamaica, that inspired Kool Herc's block parties in the Bronx to the protest outside the 2000 Democratic National Convention in Los Angeles.<p>
Some of the book's chapters diverge from the topic of hip-hop completely while others paint a vivid scene of hip-hop during its early years accompanied by insightful anecdotes. The stories within this timeline cover a wide range of events, including hip-hop's entrance into the upper-class Manhattan art scene in the mid-1980s, the anti-apartheid movement and the 1992 LA rebellion. <p>
<i>Can't Stop Won't Stop </i>reminds us that the current economic and political situation in the U.S. it is not just the product of George Bush, but a system that has targeted the poor, working-class and youth populations.<p>
However, the repression and abandonment has always produced a response at one level or another, sometimes through political activism and sometimes through hip-hop.<p>

