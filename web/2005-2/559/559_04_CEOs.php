<font face="Times New Roman, Times, serif" size="5"><b>Big business pays CEOs more than they gave in relief</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 30, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>JOE ALLEN looks at whether Corporate America deserves the credit it has gotten for helping out after Hurricane Katrina.</b><p>

CORPORATE AMERICA is getting high marks for its humanitarian response to the Hurricane Katrina disaster.<p>
According to the <i>New York Times, </i>the country's biggest companies have dug deep to donate money, personnel and goods in a "time-honored gesture of corporate goodwill." Corporate donations currently stand at around $372 million--and are on a pace to exceed business giving after the September 11 attacks and the Asian tsunami. Big business, the <i>Times </i>adds, is also bringing its "hallmark speed and efficiency to the process of sending in goods and services."<p>
Speed. Efficiency. Goodwill. It seems to good to be true. And you know what? It is.<p>

