<font face="Times New Roman, Times, serif" size="3">The $372 million in donations may sound like a lot, but not when compared to the pay of corporate CEOs. In fact, $372 million doesn't even come close to the combined pay of just the 10 highest paid CEOs of 2004--who raked in over $605 million, according to the AFL-CIO. <p>
Let's look at the pay and compensation for a CEO who didn't make the AFL-CIO's top 10 list--General Electric's Jeff Immelt--and see how it stacks ups to GE's hurricane relief. "We want the people of the Gulf Coast to know that the people of GE are with you," Immelt declared on the company's Web site. GE says that it's donating $22 million to hurricane relief (of which $3 million is employee contributions), including $10 million in equipment and services. <p>
In 2004, Immelt made $8,814,145 in total compensation, plus another $3,630,750 from the previous year's stock option that he cashed out--that's $12,444,895 for one year for one executive. In addition, Immelt has another $22,890,790 in stock options from previous years that haven't been cashed in yet and are likely to be worth much more when they are.<p>
Immelt could more than match GE's entire corporate contribution for hurricane relief from stock options he hasn't even exercised yet.<p>
Georgia-Pacific, one of the world's largest makers of paper products, says on its Web site that it donated $2.5 million to hurricane relief. But this pales in comparison to CEO and Chair Alston Correll's total pay package in 2004: $14,302,844 in total compensation, plus another $1,187,330 from previous stock options he cashed in. Like Immelt, Correll has another $19,629,224 in unexercised stock options.<p>
Meanwhile, Michael Eskew, CEO of United Parcel Service, the largest package delivery company in the world, boasted on the company's Web site that UPS was donating $1.3 million. This is less than the $1,655,332 in stock options that he cashed out in 2004. Aside from the stock options, Eskew raked in another $2,898,187 in total compensation.<p>
The top CEOs in this country have enormous wealth and resources at their disposal. Their donations, so far, are pitiful compared to what they could give. They are trying to buy "goodwill" on the cheap.<p>

