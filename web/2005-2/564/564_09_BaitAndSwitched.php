<font face="Times New Roman, Times, serif" size="5"><b>Undercover in Corporate America</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Elizabeth Lalasz</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 4, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Barbara Ehrenreich, <b><i>Bait and Switch: The (Futile) Pursuit of the American Dream. </b></i>Metropolitan Books, 2005, 237 pages, $24.</font><p>

<font face="Times New Roman, Times, serif" size="3">IN BARBARA Ehrenreich's new book, <i>Bait and Switch: The (Futile) Pursuit of the American Dream, </i>she employs the same method of investigative journalism many readers became familiar with in her 2001 bestselling book, <i>Nickel and Dimed: On (Not) Getting By in America.<p>
</i>She goes undercover and becomes an executive, white-collar job seeker in Corporate America. Ehrenreich uses a different name and "re-works" her freelance writing experience in an attempt to find a job in a public relations department. But, unlike <i>Nickel and Dimed--</i>where Ehrenreich secures several low-paying jobs as a waitress at a restaurant, a clerk at Wal-Mart and a maid at a hotel, and the reader witnesses first-hand the day-to-day demeaning horror of these jobs--in <i>Bait and Switch, </i>she's looking to land a $50,000 a year job with benefits.<p>
Considering the Bush administration's never-ending media mantras about how great the U.S. economy is doing and how many jobs are being created, this may sound like it isn't difficult. But Ehrenreich's experience reveals quite the opposite. <p>
It is the extended job-search process, in all its painful futility, which is the main focus of <i>Bait and Switch. </i>What the book reveals is that even people who have done everything "by the book" to succeed in the corporate world are being screwed in today's economy, like the poor and working class she writes about in <i>Nickel and Dimed</i>.<p>
Ehrenreich explores every trend in her pursuit for employment, including hiring a job coach, going to networking seminars and even a job "boot camp" and having an "image makeover." (There is certainly a lot of money being made off of unemployed corporate executives). Yet, despite all her effort and money spent, Ehrenreich receives no job offers for nearly seven months.<p>
"[R]ejection puts too kind a face on it, because there is hardly ever any evidence that you have been rejected--that is, duly considered and found wanting," she writes. "As the <i>New York Times </i>reported on June 2004: 'The most common rejection letter nowadays seems to be silence. Job hunting is like dating, only worse, as you sit by the phone for the suitor who never calls.' The feeling is one of complete invisibility and futility: you pound on the door, you yell and scream, but the door remains sealed shut in your face."<p>
In the end, the only job offers she receives are for two sales positions with insurance company AFLAC and Mary Kay Cosmetics. The average salary for both is in the low $30,000s with no health benefits.<br>
<i>Bait and Switch </i>isn't groundbreaking in the way <i>Nickel and Dimed </i>was by revealing how commonplace poverty-wage employment in the U.S. today. It is more an undercover confirmation of what unfortunately has become at least a decade-long trend--lay-offs and downward mobility of former middle and upper management employees.<p>
Corporations justify their actions with excuses of the need to become "lean, mean and competitive." But these actions have real, immediate and human consequences--companies leave a trail of shattered lives in their drive for higher profits. With this in mind, <i>Bait and Switch </i>is well worth reading.<p>

