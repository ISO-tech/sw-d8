<font face="Times New Roman, Times, serif" size="5"><b>"Congress is gunning for the poor"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 4, 2005 | Page 1</font><p>

<font face="Times New Roman, Times, serif" size="3">MAKE THE poor pay. That's the slogan that George W. Bush and the politicians seem to have adopted for their latest budget proposals.<p>
In a speech at the Economic Club in Washington, D.C., Bush urged Congress to "push the envelope" on spending cuts--in order to help pay for relief for victims of Hurricane Katrina and keep the federal deficit down at the same time. Bush applauded Congress for "making progress toward cuts that will show the American people we're capable of being wise about the money and, at the same time, meet our priorities."<p>
But just who comes first in setting those priorities? The bulk of the projected $50 billion in cuts over five years that the White House and the Republican leadership want will come directly from programs that ordinary people rely on: Medicare and Medicaid, the Head Start education program, food stamps, student loan subsidies and a federal home heating assistance program.<p>
Last week, the Senate Budget Committee approved $39 billion in spending reductions--including a $10 billion cut to Medicare and Medicaid. The following day, the House Agriculture Committee cut nearly $1 billion from the federal food stamp program.<p>
Head Start and Early Head Start--federal programs for low-income preschoolers--would lose $82 million in funding, leaving an estimated 35,000 children nationwide to be dropped from the programs. National Head Start Association President Ron Hernond told the press that the cuts, if they go through, could be the "death knell" for the program.<p>
And with experts warning that home heating costs across the country will rise between 30 and 50 percent this winter (and possibly higher), the Senate last week defeated proposals to increase spending for the Low-Income Home Energy Assistance program from $2.2 billion to $5.1 billion--a decision that will certainly force some people to choose between heat and food this winter.<p>
Some spending items will survive the sharpened budget ax without a scratch. The administration's new "fiscal responsibility" doesn't extend to spending in the U.S. war on Iraq, which currently amounts to $7 billion <i>each and every month</i>. And Republicans made sure to scrap "across-the-board" spending cuts--in order to preserve increases for defense contractors and all the politically connected corporate interests that will benefit from the record $286 billion highway bill recently passed into law.<p>
Neither Republicans nor Democrats are suggesting that oil companies be taxed to increase funding for heating subsidies--despite the fact that ExxonMobil announced that its third-quarter profits were up 75 percent to $9.9 billion, and several other oil giants posted similar big jumps. Asked how much pressure he was feeling about possible new taxes for the oil industry, BP chief Lord John Browne last week told reporters, "None."<p>
Meanwhile, as it blames the spending cuts on the devastation of Hurricane Katrina, the Bush administration has opposed a number of efforts to help hurricane victims, including refusing to consider extending Medicaid coverage to Katrina victims who lost their jobs or health insurance in the flood.<p>
As commentator Harold Meyerson pointed out in a <i>Washington Post </i>column, "The same Republican zealots who demand fiscal responsibility by cutting $50 billion of the indigent sick are now also demanding a new $70 billion in tax cuts, including the permanent repeal of the estate tax, which would chiefly benefit the rich." As Meyerson summed up, "Congress is back in session, and it's gunning for the American poor."<p>

