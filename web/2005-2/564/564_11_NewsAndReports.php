<p><a name="SFSU"></a><font face="Times New Roman, Times, serif" size="4"><b>Fight racism at SFSU</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Antwi Akom, an ethnic studies professor at San Francisco State University, was arrested October 25 in a clear case of racial profiling as he entered his office to pick up a book he needed.<p>
Before entering the Ethnic Studies building at 10 p.m., a security officer asked him why he was there. He responded by saying he was a professor, and he needed a book. When he came out, there was a police officer waiting for him who told him to put his hands behind his back. <p>
When Akom asked why he was being arrested, the officer had no answer for him. The two began to argue, and the police officer called for backup. The three police officers forcefully threw Akom to the ground, arrested him and charged him with two felonies--resisting arrest and assaulting an officer.<p>
This is not the first incident of police racism at SFSU. In April 2004, a 15-year-old African American high school student was beaten and arrested in front of fellow high school students by campus police, only to be found innocent hours later. "What's interesting is that [Akom] is living proof of what we teach in ethnic studies," Matthew Shenoda, one of Akom's colleagues, explained to <i>XPress,</i> the campus newspaper. "You can have all the credentials in the world, and it does not matter." <p>
<i>Jeffrey Boyette, Kristin Lubbert and Camille White-Avian contributed to this report</i>.<p>

