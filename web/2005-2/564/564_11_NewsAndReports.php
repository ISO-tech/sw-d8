<font face="Times New Roman, Times, serif" size="4"><b>Stand up for immigrant rights</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jenny Olson</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SACRAMENTO, Calif.--More than 700 counter-demonstrators came together August 29 to disrupt a racist Minuteman rally held on the State Capitol steps here.<p>
The Minuteman Project, a group of vigilantes that has been terrorizing immigrants trying to cross the border in California and Arizona, rallied to support a California Border Police initiative which would establish a state border police in addition to the existing federal border police. <p>
Organizers of the counter-protest included the Bay Area Coalition to Fight the Minutemen, Deporten a la Migra, the ANSWER Coalition and Los Zapatistas. Counter-demonstrators chanted in both Spanish and English while marching to the Capitol steps to shout down approximately 200 supporters of the initiative, including Minuteman Project founder Jim Gilchrist.<p>
A line of police blockaded the steps while counter-demonstrators remained lively and loud, chanting "racists go home" to the beat of the Brass Liberation Orchestra. For an hour, the counterprotesters marched back and forth in front of the Capitol, drowning out the Minuteman rally with their chants.<p>
A closing speak-out was held at the opposite end of the Capitol to remind immigrant rights activists of our fight ahead. "Whenever the economy gets bad, Latinos become scapegoats," an organizer told the crowd. "In reality, we create the wealth." Undocumented immigrants have some of the dirtiest, most dangerous and worst-paying jobs, but in return have very few rights. <p>
The crowd was encouraged to protest the state's driver's license policy December 12, the second anniversary of a one-day statewide labor and school strike sparked by Schwarzenegger's repealing licenses for undocumented immigrants.<p>
Immigrant rights activists need to continue confronting the Minutemen and other anti-immigrant vigilantes on the borders, organizing rallies when then they try to enter our cities with their racist rhetoric, and defeating anti-immigrant ballot initiatives. We need to unite and fight for "un mundo sin fronteras"--a world without borders.<p>

