<p><a name="AIDS"></a><font face="Times New Roman, Times, serif" size="4"><b>Campaign to End AIDS</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Conor Reed</b></font><p>

<font face="Times New Roman, Times, serif" size="3">A COALITION of 14 AIDS advocacy organizations has mobilized hundreds of activists to converge on Washington, D.C., November 4 for "Four Days of Action to End AIDS."<p>
Since the kick-off date of October 15, a dozen caravans have been heading toward Washington, making stops in almost 100 cities and towns to raise awareness about the epidemic. <p>
In Salt Lake City, activists lined up 8,500 pairs of shoes in front of the city's main library to represent the number of people who die each day of AIDS. A caravan from New York began its 22-day, 230-mile journey with a march from Times Square through the Lincoln Tunnel, connecting New York and New Jersey.<p>
Currently, more than 40 million people are living with AIDS worldwide. AIDS kills one child every minute, and 14,000 people contract HIV daily. AIDS has become a primary cause of homelessness in the U.S., and African American women between the ages of 25 and 44 are 13 times more likely to die of AIDS than women of other ethnicities.<p>
"Some people think it's hyperbole when we talk about ending the AIDS epidemic," said Charles King, of the Campaign to End AIDS and the New York City-based Housing Works. But, he added, "If they can do needle exchanges in Tehran, we can do it in this country. If they can pass out condoms in China, we can do it in our schools." <p>
In Washington, activists will hold rallies, visit lawmakers and participate in a civil disobedience action. The campaign is pushing the government to reauthorize and fully fund the recently expired Ryan White CARE Act, protect Medicaid from severe budget cuts, strengthen U.S. support for the Global Fund to fight AIDS, approve global debt cancellation and more.<p>
"By marching through these places, we're able to reach people personally and give them this message that AIDS is a serious problem that affects many people in this country and can no longer be ignored," said Daniel Solon, a Housing Works Bookstore staff member and participant in the New York to D.C. caravan.<p>
<i>For more information on the Campaign to End Aids, go to <a href="http://www.C2EA.org" target="_blank">www.C2EA.org</a> on the Web</i>.<p>

