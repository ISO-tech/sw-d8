<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">November 4, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>General Motors/Delphi</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WITH BANKRUPT auto parts maker Delphi demanding wage cuts to $9.50 per hour and General Motors set to cut retiree health care benefits by 25 percent, activists in the United Auto Workers (UAW) are planning to take the initiative in planning a fightback.<p>
A meeting set for UAW Local 1231 in Comstock Park, Mich., November 6 will focus on how to resist Delphi's attacks in advance of the bankruptcy court judge's December 16 deadline by which a contract must be negotiated.<p>
If there's no deal by that date, says Delphi CEO Robert "Steve" Miller, the company will ask the judge to abrogate labor agreements altogether. To push through the cuts, Delphi is counting on GM to honor terms of the spinoff, which would allow Delphi retirees to draw their pensions from GM--and about half the UAW members at the company are eligible for the deal. And with GM set to cut retiree medical benefits by 25 percent, top UAW officials arranged for two retirees to file a lawsuit against GM in federal court in order to pre-empt a possible class action suit by other retirees angered by the cuts.<p>
The initiative for the rank-and-file meeting came from Gregg Shotwell, a member of UAW Local 2151 at the Delphi plant in Cooperstown, Mich. The Comstock Park meeting, which is backed by the UAW New Directions Movement and the UAW Solidarity Coalition, is aimed at building a rank-and-file alternative.<p>

<font face="Times New Roman, Times, serif" size="4"><b>San Francisco teachers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>Adrienne Johnstone, United Educators San Francisco</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Several hundred members of both SEIU Local 790 and United Educators San Francisco (UESF) staged a sit-in at the San Francisco School Board meeting last week.<p>
Hoping to bring attention to a possible strike and contract negotiations that have been fruitless to date, UESF President Dennis Kelly and Lawanna Preston, chief negotiator for the Local 790 School Division, and more than a dozen others took over the meeting room, occupying the seats at the table usually reserved for board members.  <p>
At 7:30 p.m., members of the school board entered to ask that the room be cleared for the start of the regular meeting. When the union leaders refused, board president Eric Mar informed the crowd of teachers, classroom aides, cafeteria workers and janitors that the police would be called. After booing and hissing from the crowd, Mar backed down, allowing the unions to hold their meetings while the school board adjourned to a closed session nearby.<p>
Rank-and-filers passed the bullhorn and shared their stories. Elementary school teacher Jeremiah Jefferies reminded the crowd, "Our working conditions are the students' learning conditions." <p>
San Francisco Unified continues to claim poverty as justification for their unwillingness to concede to any union demands at the table. Yet district budget records show that the district has $11 million over state required reserve levels--money that should be available for the workers in the district.<p>
UESF executive board member Ken Tray inspired the crowd to continue the fight saying, "Any social studies teacher worth their mettle teaches that freedom, that liberty is never given, it is taken, it is fought for."<p>

