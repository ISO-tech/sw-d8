<font face="Times New Roman, Times, serif" size="4">Workers refuse to accept SEPTA's demand for cuts</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Strike halts Philly transit</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jaan Fulano</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 4, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">PHILADELPHIA--The two main mass transit unions here went on strike on October 31 in response to a contract dispute that has lasted almost two years.<p>
United Transportation Union (UTU) Local 1594 and Transport Workers Union (TWU) Local 234 represent more than 5,000 bus drivers, mechanics, train operators and other workers employed by the Southeastern Pennsylvania Transit Authority (SEPTA).<p>
The core issue is SEPTA's proposed cuts to health care and other related benefits. Management is demanding that workers accept a 40 percent cut in health benefits and surrender maternity leave, weekly overtime and a portion of vacation time. More substantively, SEPTA management seems intent on breaking the TWU 234, a historically militant union.<p>
While SEPTA workers are being asked to relinquish a variety of benefits, management enjoys complete health care coverage, including ample sick and vacation time. While management claims that workers will "only" have to pay 5 percent of health care costs, this isn't the whole story, according to TWU Local 234 President Jeff Brooks.<p>
TWU spokesperson Bob Bedard told reporters, "Under their health care proposal, if you or your spouse or kid ended up having to go to the hospital for five days, you'd spend your whole raise." Under management's plan, a newly hired bus driver making $25,000 per year would pay the same in health care costs as a top SEPTA executive. The union is prepared to make concessions on health care costs if it is based on a percentage of pay, so that highly paid managers and executives have to pay more--but management has rejected this proposal.<p>
Job security is another major issue. SEPTA wants the unimpeded ability  to subcontract work, the complete authority to effect layoffs, and the option to block the union from collecting authority dues.<p>
The members of TWU Local 234 and UTU Local 1594 understand the high stakes of the battle and are preparing for a protracted struggle. "SEPTA claims the main sticking point is health care, but we haven't had a raise in over two years," Bedard said. "The problem isn't the workers, but SEPTA. It's become classism with SEPTA. They look at themselves as the wealthy people from New Orleans, and the workers as the people who live in the Ninth Ward," the impoverished, predominately African American neighborhood abandoned in the flood.<p>
Bedard added, "At this point we are ready for the long fight. Concede is not a word that transit workers know." <p>
Media coverage is focused on the inconvenience the strike will cause for the almost 500,000 SEPTA riders. But there was widespread public support for the most recent TWU strike in 1998, lasted  more than 40 days and ended with a raise and some benefit protections.<p>
This could be one of the most important labor struggles in the city in recent times, and it deserves our active support and solidarity. <p>

