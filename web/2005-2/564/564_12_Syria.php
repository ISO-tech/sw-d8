<font face="Times New Roman, Times, serif" size="5"><b>White House ups pressure against Syria</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 4, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">STEPPED-UP pressure on Syria is the U.S. government's latest effort to remake the Middle East diplomatically while its war machine remains stuck in the sands of Iraq.<p>
The U.S. seized on a United Nations (UN) investigation of Syria's alleged role the assassination of Lebanese Prime Minister Rafiq Hariri earlier this year to push through a UN Security Council resolution demanding that Syrian President Bashar al-Assad cooperate with the inquiry. <p>
Having already exploited the Hariri assassination to demand Syria's withdrawal from Lebanon earlier this year--using a previous Security Council resolution to do so--the U.S. is now accusing the Syrian regime of supporting the Iraqi resistance. <p>
Thus, the <i>New York Times </i>last month published an account of U.S. troops clashing with Syrian forces on the Iraqi border. Even as the <i>Times </i>editors were criticizing their own reporter, Judith Miller, for selling the administration's fabricated pretext for the war on Iraq, the newspaper printed quotes from anonymous government sources claiming that Syria functioned like Cambodia during the Vietnam War--a refuge for guerilla fighters and conduit for their supplies.<p>
There are not many signs so far that the U.S. will launch a full-scale military onslaught in Syria--especially given the rising opposition at home to the war in Iraq. Instead, the U.S. has made France, its adversary during the Iraq war, into its closest ally in pressuring Syria, with military pressure contracted out to Israel.<p>
For France, the aim is restoration of its influence in Lebanon--its former colonial dominion along with Syria itself after the First World War. France's influence in Lebanon, exercised through the Christian minority, was eclipsed after Syria's occupation began in 1975, during the civil war between the Christians and the Muslim majority.<p>
Syrian rule over Lebanon was blessed by the U.S. after the 1991 Gulf War as a reward for Damascus' support for the U.S.-dominated coalition against Iraq. The Hariri assassination--the details of which remain murky, despite the UN report--gave the U.S. the initiative to demand new concessions. It also provided a U.S. and UN velvet glove for Israel's iron fist, seen in September 2003 with an Israeli air strike deep into Syrian territory--a move immediately followed by Congress' passage of a bill imposing sanctions on Syria.<p>
By turning up the heat, the U.S. hopes to crack open the factions around the Assad family's ruling circle and eventually achieve a "regime change" similar to the "Cedar revolution" in Lebanon, where the largely Christian middle class took to the streets to protest Hariri's assassination.<p>
This is the context that the recent anti-Israel rhetoric of Iranian President Mahmoud Ahmadinejad must be seen.<p>
Iran itself faces possible UN sanctions for its nuclear program, backed by threats of U.S. and Israeli military strikes. If the U.S. and Israel are successful in diplomatically isolating or decisively weakening Syria--Iran's closest ally in the Middle East--than Iran itself will become more vulnerable. <p>
Moreover, both Syria and Iran are allied with Hezbollah, the Shiite Islamist party in Lebanon that Israel considers to be a continuing threat--and, having militarily forced Israel to withdraw from Lebanon, a dangerous example for the Palestinian movement.<p>
Ahmadinejad's declaration that Israel should be "wiped off the map"--although moderated days later by former president Ali Akbar Rafsanjani--signals an effort by Iran to counter U.S. pressure. And since Iran wields considerable influence among the Shiite Islamist parties in the Iraqi government, the U.S. provocations continue to raise the risk of a wider war in the Middle East.<p>
UN-backed demands for "justice" in the Hariri assassination don't change the fact that imperialism is still attempting to impose its will on the Middle East--with Washington directing the agenda.<p>

