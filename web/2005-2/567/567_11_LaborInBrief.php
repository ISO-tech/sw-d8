<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">December 2, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Philadelphia transit workers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jaan Fulano</b></font><p>

<font face="Times New Roman, Times, serif" size="3">PHILADELPHIA--The weeklong strike by Transport Workers Union (TWU) Local 234 and United Transportation Union Local 1594 ended November 7 with some important gains and some setbacks.<p>
The union, which represents more than 5,000 bus drivers, mechanics and other workers employed by the Southeastern Pennsylvania Transit Authority (SEPTA), walked off the job October 31. The strike brought Philadelphia to a standstill, affecting some 460,000 commuters.<p>
The four-year contract, which members approved November 15, includes an annual raise of 3 percent and provisions for improving grievance procedures. The union also forced SEPTA management to agree to contribute a portion of their wages toward their own health plans.<p>
Many members see this agreement as a step forward and a symbol of the strength of the union. "We won some very important work rules changes that have to do with due process and fairness," said TWU representative Bob Bedard. "We also won back a few things. We think this is a victory for the members."<p>
However, the contract is not as strong as could be. It stipulates that workers pay 1 percent of health care costs, and while this is significantly lower than the 5 percent SEPTA was demanding, this will be the first time that members have had to contribute to the cost of their medical insurance premiums. Some union members consider this a significant concession, since the TWU has held a firm stance on this issue for many years.<p>
Moreover, SEPTA management's promise to contribute towards health care costs is not included in the language of the contract--indeed, it is a verbal accord that Gov. Ed Rendell has promised to enforce.<p>
Despite such shortcomings, this contract fight showed transit workers' resolve to fight SEPTA's attack on unions. "We showed our fighting spirit and resolve," said Mark Skinner, a bus driver and union activist. "We also sent a message to SEPTA that they can't trample on our rights and go unchecked."<p>

<font face="Times New Roman, Times, serif" size="4"><b>Colchester, Vt., teachers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Kristin Sweeney</b></font><p>

<font face="Times New Roman, Times, serif" size="3">COLCHESTER, Vt.--After an eight-day strike, the Colchester Education Association (CEA) won a 4 percent raise over the next three years and saved the health care plan for the town's public school teachers.<p>
Although Colchester teachers are still the lowest paid in the county, it was an inspiring victory against the anti-union school board that had insisted on a 2.5 percent raise, halving sick leave and long-term disability, and drastically raising health care costs. <p>
The board justified its actions by claiming that Colchester residents could not afford to pay higher property taxes, although the difference between the union's proposal of a 5 percent raise and the school board's paltry proposal is only a $30 difference for an owner of a $200,000 house.<p>
The success was in large part due to the public outrage against the school board. Frustrated parents flooded both sides with phone calls and e-mails, insisting on an end to the strike that kept more than 2,000 students out of school. <p>
Citizens found that the school board had put off negotiations and hid from the public. When an angry crowd of 150 parents and taxpayers came to an October 18 school board meeting, the board violated its own policy of allowing citizen participation by claiming it was a "special" secret meeting.<p>
The school board asked the teachers to end the strike and continue negotiations while in the classroom, but the union wisely refused and called for binding arbitration. The board promptly rejected this, fearing that an arbitrator would give teachers a 4.5 to 5 percent raise. <p>
As to why the school board finally reached the agreement on October 23, school board chair Renn Niquette said, "When you've got people e-mailing you, calling your home and writing letters to the editor attacking your personal integrity, that puts you on somewhat of a strain."<p>

