<font face="Times New Roman, Times, serif" size="4">John, June and making music history</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Never a deeper love"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 2, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Walk the Line</i>, </b>directed by James Mangold, starring Joaquin Phoenix and Reese Witherspoon.</font><p>

<font face="Times New Roman, Times, serif" size="3">"STEADY LIKE a train. Sharp like a razor." June Carter fittingly describes the voice of Johnny Cash--the man she would spend the better part of four decades collaborating with and married to--in the excellent film <i>Walk the Line</i>.<p>
By the time of his death in 2003, Cash was an American legend. A rebel with a cause, Cash was a country and rock pioneer who stood up for the downtrodden and made some of the most compelling music to ever hit the charts.<p>
<i>Walk the Line</i> is the story of the extraordinary love affair between Cash and Carter--spanning years of mutual attraction, failed marriages, and Cash's battles with drug and alcohol addiction.<p>
One of the film's triumphs is in how it captures a unique moment in American music history. When Cash burst onto the music scene in 1955 as part of the Sun Records stable--which included greats like Elvis, Jerry Lee Lewis and Carl Perkins--he became one of the men who would shape the emerging genre of rock and roll, and the future of country music.<p>
Backed by the sparse "boom-chicka-boom" style of the Tennessee Two--Luther Perkins on rhythmic guitar and Marshall Grant on slap bass--Cash made a name for himself with a sound that was brilliantly simple and songs that celebrated hard living and hard loving.<p>
The surprise of the movie is the skill with which actor Joaquin Phoenix approximates Cash's famous bass-baritone. In between songs, he turns in a performance that shifts from Cash's deep attraction for Carter to harrowing episodes of drug addiction.<p>
Carter--already American music royalty as part of the Carter Family Singers--would write in 2000 that, on that first tour with Cash and Elvis, Cash's "low voice just penetrated my heart and spoke to my loneliness." Later, she would pen the song "Ring of Fire" for Cash--a public testament to their love, set to one of the most memorable horn sections in music.<p>
Although the scope of Cash's progressive politics--his support for workers' rights, sympathy for Native American struggles and opposition to the Vietnam War--are only hinted at on the film's margins (as when he excitedly tells his family that he's written a letter to Bob Dylan), the film celebrates Cash's solidarity with ordinary people, particularly prisoners.<p>
In one scene, when Cash lobbies record executives to record a live concert inside the walls of Folsom prison, an executive tells Cash that his fans are Christians who don't want to hear him singing to "a bunch of murders and rapists to cheer them up." Cash doesn't miss a beat, replying that, if that's the case, "they're not Christians."<p>
In an all-too-brief scene, the movie captures the spirit of the famous 1968 concert at Folsom prison. Along with Cash's respect for the prisoners and his joy at winding up the warden, you can, as Cash wrote when the record was released, "feel the electricity and hear the single pulsation of 2,000 heartbeats in men who have had their hearts torn out, as well as their minds, their nervous systems, and their souls."<p>
While some may regret the film's focus on the relationship between Cash and Carter rather than on Cash's politics, Cash himself should have the last word on that note.<p>
 "What has happened to our love language?" he wrote in 2000, for the introduction to a compilation of his love songs. "We have brought it down to three-minute sound bites--sandwiches in cute words that rhyme. And it's a shame that those love songs are played everywhere with no follow-up kisses to seal the words...I remember when I fell into June's 'Ring of Fire,'" he continued. "There was a lot of showing it as well as saying it. Never has there been a deeper love than my love for her."<p>
<i>Walk the Line</i> is a fitting celebration of one of music's most enduring relationships--and a love that burned, burned, burned.<p>

