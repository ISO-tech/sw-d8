<font face="Times New Roman, Times, serif" size="5"><b>Solidarity wins in SF schools</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Adrienne Johnstone, United Educators San Francisco</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 2, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--The San Francisco Unified School District (SFUSD) narrowly avoided a strike when a tentative agreement was reached with Service Employees International Union (SEIU) Local 790 November 17. <p>
The agreement, which will be voted on within a couple weeks, marks a victory for SEIU and should be a boost to the teachers and paraprofessionals union, still in negotiations with the district. <p>
The new deal offers 75 percent health care coverage for dependents, up from 60 percent, and a 4 percent raise over the next two years of the three-year contract. In return, the district got language to reopen the discussion on health care in 2007. <p>
The union representing cafeteria, janitorial and office staff was prepared with strike captains, chant sheets and thousands of picket signs for more than 130 sites throughout the city. Thousands of the district's 6,000 teachers and paraprofessionals represented by United Educators San Francisco (UESF) pledged they would honor the picket lines and refuse to work during an SEIU strike. <p>
Solidarity from UESF was the deciding factor in this struggle. District administrators knew they would be unable to operate the schools in the event of an SEIU strike if UESF refused to cross picket lines. Substitute teachers are also represented by UESF, and the district was unsuccessful at lining up the number of scab teachers they would need to keep the schools open. <p>
The night before the strike was set to begin, SFUSD administrators admitted they were only able to find 127 substitute teachers willing to cross the picket lines. <p>
This represents an important step forward for all school workers in San Francisco. Local 790 was able to win gains at a time when other public- and private-sector workers are barely holding on or granting sizeable concessions. This will be an important lesson for UESF to remember as we reach the end of the road with bargaining.<p>

