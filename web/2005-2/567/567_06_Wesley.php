<font face="Times New Roman, Times, serif" size="5"><b>Racing to execute in Maryland</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Charley Grevers</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 2, 2005 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">THE STATE of Maryland is preparing to execute a death row prisoner in a case marked by racial discrimination.<p>
Maryland Gov. Robert Ehrlich has set December 5 as an execution date for Wesley Baker. Like a large majority of death row inmates in Maryland, Baker is a Black man accused of killing a white person.<p>
There are a number of holes in this case. First, to receive the death penalty in Maryland, a defendant must be the principal participant in a murder. Baker was one of two suspects in an attempted robbery that resulted in the death of Jane Tyson. The second suspect, Gregory Lawrence, had a criminal record that included a similar crime, but the jury never heard that information. <p>
The judge and jury also never heard that the gun used in the robbery had a dangerously light hair trigger--so it was easy for prosecutors to paint the picture of a premeditated act of murder, rather than a panicked failed robbery. It's not even certain that Baker fired the gun, since the results of tests to determine this were botched by the crime lab.<p>
Baker was prosecuted in Baltimore County and found guilty by an all-white jury. Seventy percent of Maryland's death row prisoners come from the mainly white Baltimore County--a percentage that is grossly out of proportion with rates of crime in the rest of the state.<p>
In 2002, former Gov. Paris Glendening commissioned the University of Maryland to investigate bias in Maryland's death penalty system. Death row prisoners, including Baker, were granted a temporary reprieve when Glendening established a moratorium on executions after the study that found significant racial and geographical bias.<p>
The moratorium ended on January 15, 2003, when the Republican Ehrlich succeeded Glendening as governor.<p>
As <i>Socialist Worker </i>went to press, Baker and his federal public defender, Gary Christopher, were waiting for the U.S. Court of Appeals to decide whether to allow a post-conviction hearing based on the results of the University of Maryland study.<p>
By signing the death warrant, Ehrlich is choosing to ignore the racism of the system. Blacks account for about 80 percent of Maryland's murder victims, yet 92 percent of those currently on death row are accused of killing whites. Also, Black-on-white crime accounts for less than 5 percent of murders annually, yet 62 percent on death row are Blacks accused of killing whites. <p>
It is especially disturbing how quickly Ehrlich wants to rush through Baker's execution. Baker's attorney stated that no death row prisoner has ever been put on such a fast track to execution.<p>
Maryland's death penalty has been proven unfairly biased. It is a racist tool used to exact retributive "justice" on poor Black men who are rarely able to adequately defend themselves. These injustices must stop. Ehrlich should commute Wesley Baker's sentence and immediately re-establish the moratorium on the death penalty. <p>

