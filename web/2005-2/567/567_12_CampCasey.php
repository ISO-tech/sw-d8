<font face="Times New Roman, Times, serif" size="5"><b>The return to Camp Casey</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 2, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>CINDY BERINGER reports from Crawford, Texas, on antiwar protests outside George Bush's vacation ranch.</b><p>

GOING BACK to Camp Casey II in Crawford, Texas, to disturb George Bush's Thanksgiving holiday felt like going home. There was the familiar smell of dried grass in a cow pasture, the big white tent welcoming protesters, and the sense of making history with the antiwar movement.<p>
The return to Camp Casey began early in the Thanksgiving week with a few dozen antiwar protesters shouting "Give me liberty or give me a ditch," and setting up tents at the site of Camp Casey I--the ditch where Gold Star Families for Peace cofounder Cindy Sheehan first made her stand this past August.<p>
The vacationing president refused to meet with Sheehan to explain the "noble cause" that her son Casey died for in Iraq, and a reinvigorated antiwar movement has been dogging him since. <p>
"We are proud to be back here," said Dede Miller, Sheehan's sister. "This is so important. What we did in August really moved us forward, and this is just a continuation of it."<p>
Miller and other campers at Camp Casey I defied new ordinances set up by McLennan County commissioners after Bush and Sheehan left, which ban camping in any county ditch or parking within a 7-mile radius of the Bush ranch. About a dozen protesters were arrested soon after setting up camp in the ditch. <p>
Among them was Daniel Ellsberg, the former Defense Department official who leaked the Pentagon Papers during the Vietnam War, revealing the U.S. government's lies to justify war that sound eerily similar today. "Those of us who finally saw through the Vietnam War saw through this war, and all the actions that were necessary to end the Vietnam War will be necessary here," Ellsberg said before his arrest, which he estimated to be his 70th since he began protesting U.S. government policies. I think the American people will get us out of this."<p>
On Thanksgiving Day, antiwar activists gathered at Camp Casey II to eat a Middle Eastern meal in solidarity with the people of Iraq. "They certainly aren't thankful for living at the hand of an illegal and immoral occupation that has killed and wounded scores of their people," said Tim Goodrich, cofounder of Iraq Veterans Against the War.<p>
On Friday, Cindy Sheehan arrived in time for the dedication of a monument to slain soldiers, relatives of members of Gold Star Families for Peace.<p>
The next day, a few hundred protesters from around the country braved the threat of rain for a rally. "We are here to end this war," said Ann Wright, the former diplomat who resigned in protest of the war.<p>
Daniel Ellsberg told the crowd that there would someday be a memorial in the ditch of Camp Casey I that would rank with the bridge at Selma, where civil rights demonstrators were attacked by police, and the bridge at Concord, site of one of the first battles of the American Revolution in 1776.<p>
Just as Ellsberg was ending his speech, protesters rushed from the tent to "greet" a presidential motorcade that was passing at a speed considerably faster than the posted limit. Several sign-carrying protesters, including one wearing the sadly familiar hood put on Iraqis tortured in Abu Ghraib, manned posts at both entrances to Bush's ranch, making it impossible for anyone to come and go without being aware of opponents of the war. <p>
Other speakers at the rally included family members of soldiers and veterans of several U.S. wars.<p>
Jeff Paterson, an ex-Marine, was a victim of a stop-loss program during the first Gulf War and the first refusnik of that war. He told of his awakening to the true nature of U.S. military interventions around the globe, and described the solidarity he received from several fellow Marines for his acts of defiance.<p>
Cindy Sheehan pointed to a picture of flag-draped coffins and said, "This is George Bush's exit strategy." She called on antiwar activists to "take it up a notch." "I think we're finally convincing Americans that Iraqis are human beings," she added. <p>
Camp Casey II will be up and running again next year, during the Easter weekend and for Bush's vacation during the month of August--and activists determined to end the U.S. war on Iraq will be there.<p>

