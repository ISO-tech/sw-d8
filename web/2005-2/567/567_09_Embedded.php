<font face="Times New Roman, Times, serif" size="5"><b>Robbins' antiwar satire now on DVD</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by John Gallagher</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 2, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Embedded Live,</b></i> a play written and directed by Tim Robbins.</font><p>

<font face="Times New Roman, Times, serif" size="3">TIM ROBBINS' <i>Embedded Live </i>is one of the best antiwar theatrical satires this year. In this hour-and-a-half play, Robbins intertwines three different storylines, giving the audience a whole host of reasons to be against the war in Iraq.<p>
First up are the nervous soldiers apprehensive about leaving their families behind and hoping that things will get better when everything is over.<p>
Then there are Bush's cabinet who appear through the play in a gang-like setting figuring out how to spin political questions to their advantage: when to start the war, how to justify it, how they need to make the decisions for the "uneducated public." In a hilariously bizarre scene, the warmongers honor their theoretical founder, uber-conservative Leo Strauss, with a mantra that leads to a crazed, erotic climax.<p>
Finally Robbins takes on the "embedded" press corps. Before they are allowed to report anything, they're given the rules of play from a military drill instructor. "We will not report the number of Iraqi deaths" and "We will not print anything not approved by U.S. military personnel" are just a few of the commands they must obey.<p>
But as more evidence about the farcical weapons of mass destruction are unveiled, even the reporters feel forced to print stories so they can keep their legitimacy.<p>
You will have a hard time finding this play live on stage, but luckily it's now available on DVD, including through Netflix.<p>

