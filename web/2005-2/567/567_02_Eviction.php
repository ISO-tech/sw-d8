<font face="Times New Roman, Times, serif" size="4">FEMA sets deadlines to drive the evacuees out of hotels</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Evicting the victims</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 2, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">WHEN 65-year-old Hurricane Rita evacuee Cynthia Sanders found a notice from the Federal Emergency Management Administration (FEMA) slipped under the door of her Newark, N.J., hotel room last month, she was devastated. "As of December 1," the notice read, "if you choose to remain in the hotel, you will be responsible for paying the bill with funds you have in hand, including any funds that FEMA has already provided to you."<p>
"What am I supposed to do, go out in the street?" a distraught Sanders--who shares the room with her 2- and 3-year-old grandsons--asked the <i>New Jersey Star Ledger</i>.<p>
Like Sanders, tens of thousands of Gulf Coast residents--many of whom lost everything during Hurricanes Katrina and Rita--are still living in hotel rooms in cities across the U.S. after having to evacuate. In mid-November, FEMA announced that it would stop paying for most evacuees' hotel rooms as of December 1--as part of a plan to "encourage" them to find cheaper, longer-term housing in apartments, trailers and mobile homes.<p>
The decision to cut off evacuees caused a public outcry, and late last month, FEMA reversed course, announcing an extension of the deadline.<p>
The approximately 35,000 evacuees living in hotels in Texas, Georgia, Florida, Alabama, California, Tennessee, Arkansas and Nevada can stay--but only until January 7. Evacuees in other states--who occupy another 3,700 hotel rooms--have an even shorter reprieve. They will be cut off on December 15.<p>
Cynthia Sanders is one of them. "I'm very, very, very upset," she said. "I just don't understand. Because FEMA tells you so many different stories, so many different things that you don't know if you're coming or going."<p>
In fact, even when they move into longer-term housing, many evacuees have received little in the way of assistance. According to a report by the Texas Apartment Association, FEMA failed to pay rent for an estimated 49,000 evacuees living in Texas. As many as 15,000 evacuees across the state were facing evictions as of November, according to the organization.<p>
FEMA's drive to force evacuees out of temporary hotel housing is seen by many activists as part of the attempt to remake New Orleans without tens of thousands of the city's poor and Black residents. That's because as more evacuees are forced out of hotels and into long-term housing, they are less likely to return.<p>
And that's something that wealthy developers and landlords are counting on. For them, Hurricane Katrina was more than just a natural disaster--it was an opportunity to rebuild the city on their terms. <p>
Within the city itself, many residents face what they say is deliberate neglect of predominantly African American neighborhoods like the Ninth Ward--as well as mass evictions from public housing and section 8 housing, justified in the name of "storm damage."<p>
John McDonald, a student at the University of Vermont, traveled to New Orleans during the Thanksgiving week to volunteer with the grassroots community activist group Common Ground, which has been organizing to defend the rights of residents. As McDonald told <i>Socialist Worker</i>, many of the poor and working-class residents who managed to remain in New Orleans feel as though they are being pushed out.<p>
"The thing that's most stark is the fact that the entire Ninth Ward has been totally neglected by all federal authority," said McDonald. "They went through and cleaned up the streets, and that's about it. It just seems like they're trying to discourage people from even coming back. That's the sense that you get from talking to people. They feel like they're totally abandoned."<p>
Meanwhile, said McDonald, in the more affluent areas of the city--even in sections that experienced flooding--streets have been cleared and residents have moved back into their homes. "So people got back in right away, and there's no talk of bulldozing those places at all," he said.<p>
In the city's poorer neighborhoods, says McDonald, it's not just neglect that's forcing people out of their homes. "In addition to FEMA evicting people from hotels, a lot of people are being evicted from their homes," he said. "Every single person I met was either facing eviction, or knew someone who was."<p>

