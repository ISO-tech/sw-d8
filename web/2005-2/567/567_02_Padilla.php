<font face="Times New Roman, Times, serif" size="4">Feds have to come clean about Jose Padilla</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Dirty bomb" deception</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 2, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">AFTER MORE than three years in a military prison in South Carolina, "war on terror" prisoner Jose Padilla was finally officially charged by the U.S. Justice Department last week.<p>
But the charges have almost nothing to do with the allegations the Feds spread about Padilla when he was arrested in May 2002 for supposedly being part of an al-Qaeda plot to detonate a radioactive "dirty bomb" in the U.S. The crimes Padilla is now accused of committing are relatively minor.<p>
At the time of his arrest, then-Attorney General John Ashcroft made a special announcement about Padilla's capture, calling him "a known terrorist" and "al-Qaeda operative" who was "planning future terrorist attacks on innocent American civilians." But as one government official told <i>Newsweek </i>magazine at the time, the case against Padilla was "blown out of all proportion."<p>
Last week's indictment confirms it. Skipping any mention of the supposed "dirty-bomb" plot, the government's 30-page indictment portrays Padilla as a minor figure, and charges him with conspiracy and providing material aid to terrorists. Among the acts that are supposed to prove Padilla's participation in a "conspiracy" to commit murder and kidnapping outside the U.S. is his 1996 application for a passport.<p>
As an enemy combatant, Padilla was held incommunicado for more than two years--barred from communicating with his family, his lawyers or any non-military personnel on the grounds that he posed a threat to national security. And if the Bush administration had gotten its way, Padilla would have remained out of sight.<p>
Instead, Padilla was finally indicted just days before a deadline for the government to submit papers to the U.S. Supreme Court regarding Padilla's legal challenge to his status as an "enemy combatant." Rather than allow the court case to proceed--which could have threatened the administration's indefinite detention of prisoners of the "war on terror" in Guant&aacute;namo Bay, Cuba, and elsewhere around the globe--the administration chose to move forward with the lesser charges.<p>
"In one respect, I'm thrilled by this decision, because from Day 1, I've been saying, 'Indict my client,'" Donna Newman, one of Padilla's lawyers, told the <i>New York Times</i>. "But on the other hand," she added, "the timing is somewhat suspect, and I think it's egregious when Attorney General Gonzales says that Padilla's being held for three-and-a-half years in solitary confinement is irrelevant. I think it's very relevant to how we are viewed around the world, and to what authority the president has to detain any citizen, almost on a whim, without charging him."<p>

