<font face="Times New Roman, Times, serif" size="4"><b>Fight police brutality</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Rayyan Ghuma</b></font><p>

<font face="Times New Roman, Times, serif" size="3">COLLEGE PARK, Md.--University of Maryland undergraduates from Community Roots, the Black Student Union, the National Association for the Advancement of Colored People (NAACP), the Peace Forum, the International Socialist Organization and many other student associations protested against police brutality November 21. <p>
Community Roots, a coalition of various student activist groups, organized this walkout in response to the November 11 police assault of George Smalls and Mallory Watkins, two African American students. <p>
Officers broke up Smalls and Watkins' weekend party at their campus apartment due to "excessive noise." The two students quickly and peacefully cooperated with police demands. But officers still felt the need to assault, mace and arrest them, but not before they put a gun to Watkin's head.<p>
Students mobilized in defense of Smalls and Watkins and walked out of Monday classes. Chaz Romeo Ball, the second vice president of the NAACP Maryland chapter and a member of Community Roots, addressed the crowd of 150 students. <p>
"Today we have three central goals," shouted Ball, "to bring awareness to the racial injustice within the nation, to expose underlying racial tensions, and to prevent future incidents of police brutality."<p>
The protesters demanded that Kenneth Krouse, the head of campus police, drop all charges against Watkins and Smalls and issue them a formal apology. "What is rebellion?" asked Ball, quoting playwright Albert Camus. "A man who says no." We say no to police brutality.<p>

