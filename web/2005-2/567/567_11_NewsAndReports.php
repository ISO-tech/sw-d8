<p><a name="Free"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend free speech </b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Allison Andrelchik</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN DIEGO--About 40 San Diego State University students gathered November 10 to protest the limitations of the so-called "free speech zones" that exist in the California State University system. <p>
A coalition of students came together from Movimiento Estudiantil Chicano/a de Aztl&aacute;n, Campus Antiwar Network, African Student Union and the International Socialist Organization to fight the cause. The campus police harassed members of each of these groups in recent weeks and months, claiming that students were being too noisy or in the wrong location on campus.<p>
The speakout started at the "Free Speech Steps" on campus--the only permitted place for students to gather and hold events, according to campus police. After marching to several campus buildings, the group paused in front of the police station while an individual harassed by police at an earlier event spoke about the importance of fighting repression on campus and demanded the university take disciplinary actions against the officers responsible.<p>
The school has begun to review its policies on free speech and how they limit students, but the struggle is not over, and this coalition will continue the fight for our constitutional rights on campus.<p>

