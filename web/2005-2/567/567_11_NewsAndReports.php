<p><a name="NO"></a><font face="Times New Roman, Times, serif" size="4"><b>No to war and occupation</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ACTIVISTS AT San Diego State University protested the war and military recruitment in schools for the November 17 national "Not Your Soldier" day of action.<p>
The event began at the Free Speech Steps with activists from the Campus Antiwar Network performing skits that mocked military recruiters and exposed the lies and injustices of their recruitment tactics. The skits were followed by speeches on U.S. militarism and imperialism, the poverty draft and the military's anti-gay "don't ask, don't tell" policy.<p>
We then marched through campus, and because the ROTC office doors were open, we marched silently through the office with antiwar signs and rainbow flags. The campus police showed up shortly afterward to try to break up our protest, but we continued to march through the campus chanting.<p>
-- In <b>Providence, R.I., </b>almost 40 students at Central and Classical High Schools have signed opt-out forms requesting that their names, phone numbers and addresses be withheld from military recruiters.<p>
Central's student body is overwhelmingly minority, a primary target for military recruiters. Opt-out signing outside the school can lead to challenging military recruitment within the school walls. For information about counter-recruitment efforts in Providence, call 401-453-1243.<p>
<i>Jocelyn Blake and John Osmand contributed to this report</i>.<p>

