<font face="Times New Roman, Times, serif" size="5"><b>UAW sells out its retirees</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 2, 2005 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">NOVEMBER 13 will go down in infamy as the day the United Auto Workers Union (UAW) sold out its retirees. <p>
UAW President Ron Gettelfinger and his gang of scoundrels on the International Executive board authorized a sham vote on contractually negotiated health care benefits without permitting us UAW retirees, who helped ratify these health benefits and agreements, the right to vote on these concessions.<p>
Gettelfinger expects retirees to pay out nearly $800 more per year for health care. This is a de facto cut of our pensions by the same amount. This hurts all UAW members, but hurts the retirees the most. In effect, Gettelfinger, our so-called union leader, did what Ronald Reagan, George W. Bush and the rest of the neo-cons have been trying to do for years--take away the voting power of the working class.<p>
Retirees are not afraid of a struggle. We have struggled and sacrificed in the past to make a better life for our families and all Americans. We will continue to struggle for greater economic justice in the future.<p>
It is just a shame that our so-called union leaders have so little trust in us retirees that they felt the need to strip our democratic right to vote on this important issue. I've got some news for them: We have not yet begun to fight!<br>
<b>Joseph Guadagno, </b>UAW Local 774, retired, West Seneca, N.Y.<p>

