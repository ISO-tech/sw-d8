<font face="Times New Roman, Times, serif" size="5"><b>When curing people isn't profitable</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 18, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">THANK YOU for the excellent piece on the threat of an Avian Flu pandemic ("Bird flu: The nightmare scenario," October 14). <p>
In the past few weeks, media coverage of this emerging threat has moved up a notch, and we are gradually discovering that there is indeed reason to be scared. Several reports are now stressing the severe lack of stocks of Tamiflu, especially in poorer regions of the world.<p>
But even here, the drug companies and the media can find cause to be cynical and calculating. One National Public Radio report even talked about how profitable it would be for Roche's rivals if the virus mutated into something that Tamiflu couldn't kill but a rival drug could!<p>
More importantly, it turns out that the Indian drug company, Cipla, plans to produce a generic version of Tamiflu, even if it goes against patent laws.<p>
While AIDS drug cocktails in the U.S. cost $12,000 a year, Cipla announced in 2001 that it would offer those same drugs for the price of $600, and sell them to Doctors Without Borders for $350. Back then, the <i>Wall Street Journal</i> ran the following headline: "Drug Industry, AIDS Community Is Jolted by Cipla AIDS-Drug Offer." (They forgot that the AIDS community were quite likely overjoyed, not "jolted," to hear this news!)<p>
The same article reported that in May 2000, five multinational drug companies had, under "intense pressure," agreed to "sharply reduce" their prices for drugs sold to sub-Saharan African countries. At the time Cipla made its announcement, only three African countries had signed on. One of them was Senegal, which agreed to a price of $1,008, nearly twice the price that Cipla brought to the table half a year later. <p>
So the business press went beserk, unable to figure out a way to condemn Cipla and not look like monsters.<p>
On May 27, 2004, the World Health Organization (WHO) disqualified two Cipla drugs as unfit to "prequalify" for WHO approval. The stated aim of the "prequalification" process is to sift out those drugs that do not meet international quality standards. But surely one would think that quality standards in India's pharmaceutical industry are at least as rigorous as those in Europe or the U.S., if not more so.<p>
Now that Cipla's drugs were deemed "unfit," and under pressure from AIDS activists, particularly in South Africa, GlaxoSmithKline granted Cipla a license to produce generic versions of its drugs on December 15, 2004. The multinationals lost some ground back then, as Glaxo settled for a mere 5 percent in royalty fees.<p>
But these are the sorts of machinations we can expect this time around, as the drug companies and their political friends in high places gamble on our lives.<br>
<b>Nagesh Rao, </b>New Jersey<p>

