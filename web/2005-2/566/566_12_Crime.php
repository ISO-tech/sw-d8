<font face="Times New Roman, Times, serif" size="4">A murder victim's son talks about real justice</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>What to do about crime</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 18, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">ON OCTOBER 15, I was at the Millions More March in Washington with the Campaign to End the Death Penalty. <p>
It was a good time, and we were productive. We had people sign our petitions to stop the execution of Stan Tookie Williams and sold out of our copies of the <i>New Abolitionist</i>. We also got into conversations with people about the death penalty and the criminal "injustice" system--like how racist they thought it was and what steps need to be taken.<p>
During these discussions, one man signed my petition, and we started talking. He asked me a very startling question: What sentence would I give a murderer? <p>
My answer at first was uncertain, but then I was just brutally honest. Why would that question shock me so much? Because it's one I've thought about ever since July 14, 1987, when I became a son of a murder victim. <p>
My father was murdered--and nobody was arrested. I thought all these years, "Was that how much he's worth?" I also was mugged and jumped multiple times as a teen. Needless to say, I was angry at the world, and engaged in very reckless behavior, having no regard for anyone else or myself--all because I wanted my father's killer(s) to be caught, sent to prison and suffer, even if it meant the death penalty. To not have these feelings initially would mean I wasn't human. <p>
Things have changed since then. When I discovered the International Socialist Organization in 2002, they taught me that sending someone away for murder, or any other crime, doesn't really help them. In most cases, incarceration makes people more ruthless and frigid. And because of what they <i>don't</i> learn while incarcerated--like skills so they can find a job or how to channel all that energy into something positive--the chances of their being rearrested skyrocket. <p>
I started asking questions like: What's the point of prison anyway? Who determines what crimes are? And who are these politicians making America "safe" for? Not for me, or for people who live in neighborhoods like mine. <p>
As long as we have this "tough-on-crime" attitude--meaning building twice as many jails as schools; slashing prisoner programs and studies, which prevents ex-cons from finding work upon release; and not focusing on more programs for families of crime and murder victims so we can try to heal--it will all be about filling up these prisons as fast as possible, while the politicians and corporations profit off of people languishing, while they've long ago served their "punishment." <p>
The only way I'd be for the death penalty or any "tough-on-crime" policy is if executing the killers could bring my father back to life. This is the real world, however. <p>
Before we can eliminate the death penalty and the criminal injustice system as we know it, we must change the perception of "criminals" by focusing on their socio-economic backgrounds, not labeling them by the crimes they've "committed." We must give them better chances to succeed, and examine what crimes really are, in a human sense. <p>
After our discussion, the man--looking stunned at the answer I gave him--shook my hand, hugged me and walked away.<br>
<b>Dan Clemente, </b>New York City<p>

