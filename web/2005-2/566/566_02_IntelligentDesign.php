<font face="Times New Roman, Times, serif" size="5"><b>Turning teachers into preachers</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 18, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">RIGHT-WING televangelist Pat Robertson wants to teach Dover, Pa., a lesson--and he doesn't mean a science lesson.<p>
Dover became a focus in the debate over the teaching of the theory of evolution when its residents voted out the town's entire school board November 8--all eight of them supporters of "intelligent design."<p>
"I'd like to say to the good citizens of Dover: if there is a disaster in your area, don't turn to God, you just rejected Him from your city," Robertson warned on his <i>The 700 Club</i> TV program. "And don't wonder why He hasn't helped you when problems begin, if they begin. I'm not saying they will, but if they do, just remember, you just voted God out of your city. And if that's the case, don't ask for His help, because he might not be there."<p>
Intelligent design theory, or "creation science," puts Charles Darwin's theory of evolution into question by arguing that certain things in nature are too complex to have evolved through natural selection. Therefore, they had to have been created by "design"--by God.<p>
If anyone has a design, it's the Christian Right--their design is to bring religion back into the schools.<p>
In October 2004, Dover became the first school district in the country to work intelligent design into the curriculum, specifically ninth-grade biology, when the school board voted to begin classes concerning evolution with a statement about "gaps" in Darwin's theory. In response, the parents of 11 kids, backed by the American Civil Liberties Union, filed a federal lawsuit, which is expected to be decided before the end of the year.<p>
As targets of Robertson's wrath, Dover's pro-science parents are in good company. Last summer, he called for the assassination of Venezuela's left-wing President Hugo Ch&aacute;vez. And in 1998, Robertson threatened Orlando, Fla., with hurricanes, earthquakes and terrorist attacks after the city allowed the display of rainbow flags in support of sexual diversity.<p>
Likewise, Dover isn't the only place where the Christian Right wants to turn the Bible into a textbook. The same day as the Dover election, the Kansas Board of Education voted 6-4 to teach "doubts" about evolution. Some 20 legislatures around the country have proposed similar intelligent design curriculums.<p>
But as Dover shows, parents and students aren't going to be taking it sitting down. Likewise, professor and parent Carol Reeves knows what she will do if Indiana lawmakers go down that road. "I tell you, I'd be down there filing a lawsuit so fast if somebody began teaching my kid intelligent design," Reeves told the <i>Indianapolis Star.</i> "This," she said, "is not science."<p>

