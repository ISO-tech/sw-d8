<font face="Times New Roman, Times, serif" size="4">Italian documentary reveals:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>U.S. used chemical weapons on Falluja</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 18, 2005 | Page 1</font><p>

<font face="Times New Roman, Times, serif" size="3">THE U.S. military denies having used chemical weapons in the war on Iraq. But new evidence revealed in an Italian documentary shows that large quantities of white phosphorus shells were used by U.S. forces during the siege of Falluja a year ago.<p>
"I heard the order to pay attention because they were going to use white phosphorus on Falluja," says former soldier Tom Englehart in the film <i>Fallujah: The Concealed Massacre, </i>which was broadcast November 8 on Italian TV channel RAI. "In military jargon, it's known as Willy Pete. Phosphorus burns bodies. In fact, it melts the flesh all the way down to the bone...Phosphorus explodes and forms a cloud. Anyone within a radius of 150 meters is done for."<p>
The documentary shows photos, obtained from the Studies Center of Human Rights and Democracy in Falluja, of the charred bodies of Iraqis.<p>
In an interview, biologist Mohamad Tareq explains, "A rain of fire fell on the city, the people struck by this multicolored substance started to burn. We found people dead with strange wounds, the bodies burned, but the clothes intact."<p>
The film also shows that the U.S. used Mark 77--a new generation of napalm, made from kerosene-based jet fuel and polystyrene which, like napalm, sticks to everything, including skin--in Iraq in 2003.<p>
The military's use of white phosphorus and napalm are in violation of the United Nations Convention on Certain Conventional Weapons of 1980, which restricts their use to military targets alone. The U.S. never signed this agreement.<p>
Just after the bloody raid on Falluja, a few news sites, such as Islam Online, reported the use of chemical weapons by the U.S. But the Pentagon denied the allegations, claiming that the phosphorus shells were used "very sparingly in Falluja, for illumination purposes," as stated on the Department of State's Web site on December 2004.<p>
However, the department had to "update" its statement last week, admitting, "White phosphorous shells, which produce smoke, were used in Falluja not for illumination but for screening purposes, i.e., obscuring troop movements."<p>
According to an article, "The Fight for Fallujah," in the March-April 2005 edition of <i>Field Artillery </i>magazine, phosphorus was used "as a potent psychological weapon against the insurgents in trench lines and spider holes."<p>
Saddam Hussein is scheduled to go on trial this month for atrocities committed against the people of Iraq--including the poison gas attack on Kurds during the Iran-Iraq war. When will the U.S. government answer for its war crimes?<p>
