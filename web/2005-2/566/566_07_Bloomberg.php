<font face="Times New Roman, Times, serif" size="4">Two-thirds of New Yorkers didn't vote</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bloomberg's triumph?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 18, 2005 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>DANNY KATCH looks at the reasons behind Mayor Michael Bloomberg's landslide re-election.</b><p>

THE RESPONSE of the media to Michael Bloomberg's landslide re-election as New York City mayor over Fernando Ferrer has been nothing short of rapturous. Bloomberg's victory was, according to the <i>New York Times</i>, "a triumph of competence over... ideology, ethnic politics and partisan appeals." <p>
Barely mentioned amid the celebrations was how few New Yorkers actually voted, despite--or perhaps because of--Bloomberg's unprecedented $74 million campaign. More accurate headlines would have read, "65 percent Stay Home, 21 percent Vote for Bloomberg" or "Bloomberg Wins--Spends $100 a Vote." <p>
Instead, the white billionaire's victory over the Latino Democrat has been cited as proof of the failure of "ethnic politics." This is ludicrous on many levels, the most obvious being that Bloomberg, who won every white district in the city, is just as "ethnic" a candidate as Ferrer. <p>
More importantly, the majority of New Yorkers are working-class Blacks and Latinos who need more, not less, "ethnic politics"--if that phrase has anything to do with addressing the city's growing racial oppression. <p>
If these sound like strong words, consider the following: The Civil Rights Project reports that schools in New York state are the fifth-most segregated in the country--meaning that most Black and Latino students go to separate and unequal schools. <p>
Meanwhile, racial profiling has been taken to new levels with Bloomberg's new policy of random searches on the subways, an alleged anti-terror measure that is actually just a brazenly unconstitutional way for cops to make petty drug busts. <p>
Perhaps the most shocking measure of racism came last year when a study by the Community Service Society revealed an unemployment rate among Black men of almost 50 percent. After the report was released, Black City Council member Charles Barron spoke for many when he asked, "How on God's earth could we hear...State of the City Addresses from both the mayor and speaker of the City Council, and no one mentioned this crisis? It's like we don't even exist." <p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">BUT POLITICIANS like Barron don't win the Democratic Party's fundraising competitions--or, as they're formally known, primaries. Instead, New Yorkers were stuck with the dreary candidacy of Fernando Ferrer, whose main qualification for mayor was his seniority in the Democratic Party machine. <p>
Any hopes in Ferrer's commitment to fighting racism were dashed in March when he told a meeting of police sergeants that the infamous 1999 NYPD murder of the unarmed African immigrant Amadou Diallo was "not a crime." <p>
This wasn't the simple campaign gaffe that the media reported it to be. The Diallo murder and the massive protests that followed are symbols of racism and the fight against it in New York. Ferrer's comments told people of color which side he was on, and he never had a chance after that. Bloomberg ended up winning 47 percent of those African Americans who bothered to vote. <p>
Amazingly enough, Ferrer was the Democrats' attempt to regain the loyalty of Black New Yorkers after the disastrous campaign of Mark Green four years ago--when Green staffers flyered white neighborhoods with grotesque racial cartoons of Ferrer kissing the ass of Al Sharpton, who had led the protests after Diallo's murder. When Green refused to fire those responsible for the flyers, many Black activists called for a protest vote for Bloomberg--a major reason the white Republican got into office in the first place. <p>
Thus, the Democrats have actually lost the past two elections because they have spit at "ethnic politics" in a city that is majority Black and Latino and where poverty has increased each of the last four years. <p>
Interestingly, there were stories in the media last week about the entrenched racism of society and the failure of politicians to respond. But the stories were about France--in the wake of the riots that spread across the country. Barely any mainstream commentators raised the obvious point that all the conditions that led to the French rebellion exist in New York City. That wouldn't have fit in with the happy theme of a city united beyond Mayor Mike. <p>
But activists should be clear that this election was actually a sign of just how disconnected both Republicans and Democrats are from the growing crisis for people of color in New York--and that the media's "last word" on the subject could turn out to be more like famous last words. <p>

