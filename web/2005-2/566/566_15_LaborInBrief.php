<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">November 18, 2005 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>San Francisco teachers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Adrienne Johnstone, United Educators San Francisco</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Some 1,200 members of Service Employees International Union (SEIU) Local 790 who work for the San Francisco Unified School District (SFUSD) were set to go on strike as <i>Socialist Worker </i>went to press. The union, which represents school secretaries, cafeteria workers and janitors, is deadlocked in a struggle over health care, with the board seeking to end health care for dependents.<p>
The school district has gone on the offensive over the last week, attacking both SEIU and the teachers' union, United Educators San Francisco (UESF). Superintendent Arlene Ackerman sent a memo to district families claiming that, in order to meet the demands of both unions, 20 schools would have to be closed.<p>
In a move designed to intimidate workers and scare families, she put another 20 district schools on a potential closing list for next year. Administrators have begun demanding that teachers turn in classroom keys and prepare emergency lesson plans in case of strike.<p>
UESF has called for its teachers to honor the SEIU picket lines in solidarity with striking workers. Many in UESF will take up this call, as teachers have come more to see that our struggle for a contract is tied to the fate of the SEIU workers and their contract.<p>
At a UESF general membership meeting on November 10, SEIU Local 790 Executive Director Josie Mooney promised that, in return for teachers' support on the SEIU picket lines, SEIU members would honor the teachers' picket in the event of a teachers' strike. Out of 6,000 members, 1,374 turned out to vote for or against scheduling a strike vote. Only 173 ballots were cast against the strike vote.<p>
This is a big step forward for UESF and shows that the members are prepared to consider striking in order to force SFUSD to realign its spending priorities. The strike vote should be scheduled as soon as possible to maintain the growing momentum and show the district that we mean business.<p>
Honoring the picket lines of our brothers and sisters in SEIU and building relationships among the rank and file of both unions is the first step toward building a broader fight for public education.<p>

<font face="Times New Roman, Times, serif" size="4"><b>The Soldiers' Home in Holyoke</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Gary Sroka and James York</b></font><p>

<font face="Times New Roman, Times, serif" size="3">HOLYOKE, Mass.--About 30 nurses, health care providers and their supporters gathered for a press conference and rally at the Soldiers' Home in Holyoke on Veterans' Day.<p>
The health care providers, who have been without a contract for more than 850 days, are members of the Massachusetts Nurses Association Unit 7, which is comprised of more than 1,800 health care workers who work in various facilities throughout the state.<p>
Many nurses point out that it's difficult to retain experienced staff because of the sub-standard wages and poor working conditions. "I didn't go to nursing school to be paid like a newspaper carrier," said Cheryl Funk, a worker with 22 years at the Soldiers' Home.<p>
Most residents living at the Soldier's Home have chronic illnesses or are disabled. Yet these veterans, whom the politicians say they honor, are routinely passed over. "It has taken the spouse of a patient writing her congressman for years to get air conditioning into the patients' rooms, and now they are saying it's coming, but it'll be another six years," said one worker. "And the patients go without television. Imagine hospitals or nursing homes these days with out television."<p>
Last July, the administration of Gov. Mitt Romney came to the bargaining table with more than 100 proposals to cut benefits, and a separate proposal that included almost no pay increase. The administration has also sought to strip the nurses and health professionals of all their rights to have input on staffing conditions at the facilities.<p>
It's going to take more actions and solidarity to force the Romney administration back to the table.<p>

