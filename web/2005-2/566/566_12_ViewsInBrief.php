<font face="Times New Roman, Times, serif" size="4"><b>Presumed guilty in NYC</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I HEARD that in the recent trial involving random searches of subway passengers, the New York Police Department Deputy Commissioner said, "In a perfect world, all the people who enter the subways would have their possessions searched." I am against the idea of guilty until proven innocent. In my idea of a perfect world, no one would be searched, because everyone would be innocent until proven guilty.<br>
<b>Chuck Mann, </b>Greensboro, N.C.<p>

