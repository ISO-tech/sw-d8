<p><a name="Life"></a><font face="Times New Roman, Times, serif" size="4"><b>High life for charity boss</b></font><p>

<font face="Times New Roman, Times, serif" size="3">JOE ALLEN'S article on the Red Cross is powerful ("The truth about the Red Cross," October 21). <p>
If possible, I think that Allen should look up the salary for the director of the Red Cross. I read somewhere that it was well above  $500,000, as contrasted with the $17,000 annual salary for the director of the Salvation Army. If folks knew about the salary for the Red Cross director, that would be highly persuasive to stop them from mindlessly writing checks!<br>
<b>Cecilia Burciaga, </b>from the Internet<p>

