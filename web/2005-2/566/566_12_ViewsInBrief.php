<p><a name="Bigot"></a><font face="Times New Roman, Times, serif" size="4"><b>Anti-choice but not a "bigot"</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NICOLE COLSON would do herself a favor by ceasing to label pro-lifers, liberal (such as myself) or conservative, "anti-choice bigots" ("Have Democrats surrendered on abortion rights?" February 4). <p>
Why? Easy...a very famous liberal, by the name of Martin Luther King Jr., was staunchly pro-life. Was he an anti-choice bigot? <p>
Dick Gregory, a close friend of Dr. King, said that "the deliberate killing of Black babies by abortion is genocide--perhaps the most overt of all." Was he an anti-choice bigot? Decades later, he said: "I fully support the right to life of every human being, from conception until natural death. In addition, I unequivocally endorse a total human life amendment to the U.S. Constitution, that would promote the value and dignity of every human life." <p>
Fannie Lou Hamer, a feminist icon, once said: "The methods used to take human lives, such as abortion, the pill, the ring, etc., amount to genocide. I believe that legal abortion is legal murder."<p>
The real bigots are those who sanction the brutal murder of innocent unborn children and cover up their crimes by applying the most ridiculous of euphemisms (e.g. "pro-choice"). No true liberal can support infanticide. Shame on Democrats, the once great liberal party, for kissing ass to the pro-abortionists.<br>
<b>E. Aviles,</b> from the Internet<p>

