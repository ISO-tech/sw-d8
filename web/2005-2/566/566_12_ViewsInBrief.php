<p><a name="Power"></a><font face="Times New Roman, Times, serif" size="4"><b>Who has real power in society?</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I WAS flipping through channels recently and came across a "Labor News" segment on a public access channel. What I saw reminded me who has the power in society.<p>
The scene was of thousands of workers, mostly low-paid Black and Latino health care workers, some making less than $7 an hour. On one side of the screen were the workers with pro-labor signs. On the other side was a stage. <p>
On this stage was 1199 President Dennis Rivera, flanked by Sen. Chuck Schumer (D-N.Y.), SEIU President Andrew Stern, and the cowardly flip-flopper Howard Dean. All of these "leaders" gave 20-second speeches about how horrible it was that the workers had to live on poverty wages.<p>
The rally took place on the first day of a three-day strike in June 2004, and after the various speeches, Rivera got up and talked about how great it was that all the workers were on strike, and how it was necessary to fight against the greedy bosses. Then, he told the workers, all 15,000 of them, that he was going to propose that they end the strike and give the union higher-ups 30 days to negotiate. All the workers screamed back at Rivera and the other hacks on stage "no."<p>
Rivera then tried to scare the workers by reading to them a statement from billionaire Mayor Michael Boomberg that told the union "to end its job action and return to work in order to minimize the disruption of patient care." Rivera then looked back at Howard, Chuck and Andy, and you could hear him saying, "I wasn't expecting this." <p>
He tried to convince the workers, at least three more times, to stop striking and go back to work, and each time, he had to stop because he couldn't be heard over the chorus of "No...no..." Rivera then said that he would not end the strike. <p>
Workers are the majority in this world, and when we refuse to take it any more, there is nothing the Republicans or the Democrats can do to us. We are too many, and we run society. They don't do crap, except lie to us and steal our labor.<p>
This was one of the most beautiful things I have ever seen in my life. In the face of the mayor of New York, pro-war Democrats like Schumer and Dean, and their own president, the workers looked all these "leaders" right in the eyes and said "no."<p>
Watching this reminded me of what it will look like when the workers of the world refuse to accept concessions any longer, and when we will push out of the way anyone who wants to continue to oppress and exploit us, be it Republicans, Democrats or union bureaucrats.<br>
<b>Nick,</b> New York City<p>

