<font face="Times New Roman, Times, serif" size="4">Feds' star witness backfires</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Witch-hunt trial of Sami Al-Arian</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 18, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">AFTER NEARLY five months of testimony, the jury was deliberating in the case against former University of South Florida professor Sami Al-Arian, as <i>Socialist Worker </i>went to press.<p>
Al-Arian and three co-defendants--Hatem Fariz, Ghassan Ballut and Sameeh Hammoudeh--plus five others charged in absentia, are accused of racketeering, conspiracy and providing material aid to "terrorists." Specifically, the Feds accuse the men of using an academic think tank, a Muslim school and a charity as a cover for raising money for the Palestinian Islamic Jihad (PIJ), which allegedly used the money to carry out violent acts.<p>
But defense attorneys for Al-Arian and the others say the money went for charity in the Occupied Territories. The U.S. didn't even consider PIJ a "terrorist organization" until 1995--and at that time, government investigations into Al-Arian's activities turned up no wrongdoing.<p>
But because he continued to speak out against Israel's oppression of Palestinians--and the U.S. government's support for Israel--Al-Arian became a target again following the September 11 attacks.<p>
Al-Arian has been behind bars since February 20, 2003. Prosecutors finally went to trial this year, believing they had a slam-dunk case. <p>
The tide of public sentiment was against Al-Arian, thanks to a smear campaign led by University of South Florida President Judy Genshaft and Florida Gov. Jeb Bush. In fact, during jury selection, more than 150 of the first 328 prospective jurors had to be disqualified for displaying blatant bias against Al-Arian.<p>
Prosecutors must have felt confident that the judge presiding over the case, James Moody, was on their side as well. Moody allowed prosecutors to depict graphic bomb scenes and present the stories of family members of Israelis killed by suicide bombers, without the ever having established a connection between the violence and Al-Arian and his codefendants. Moody has also barred the defense from talking about or showing scenes of the deaths of Palestinian civilians caused by Israeli military forces.<p>
Yet all along, the case against Al-Arian and his co-defendants has been entirely circumstantial. The <i>St. Petersburg Times</i> admitted that the government presented "no evidence linking the defendants to raising money to support the violent acts of the Palestinian Islamic Jihad."<p>
During closing arguments, prosecutor Cherie Krigsman resorted to telling jurors that it was "common sense"--not hard evidence--that should lead them to find that Al-Arian was a "professor by day and a terrorist by night." <p>
The weakness of the case became apparent when one of the Feds' supposed star witnesses backfired on them in spectacular fashion.<p>
While questioning former University of Mississippi math professor Abdul Raouf Dabus, who has known Al-Arian for years, prosecutor Terry Furr implied that a 1994 conversation between the two--in which the men discussed to what degree the PIJ, Hamas and the Palestinian Liberation Organization were participating in peace talks--proved that Al-Arian was a PIJ insider.<p>
But Dabus repeatedly denied the allegation. He said that the donations he knew about, which had come through Al-Arian's co-defendant Sameeh Hammoudeh, "went for a bookstore and an ambulance."<p>
Then, Dabus told the court that he feared that Palestinians could no longer speak their minds in the U.S. "Our kids, will they have a future here? I don't know," he said, as several jurors and onlookers were moved to tears.<p>
As Al-Arian's attorney, Bill Moffitt, told the jury during closing arguments, "The fact that [Sami Al-Arian] is educated and has said what he wanted for his people is being used against him. If he failed to speak up, we wouldn't be here."<p>

