<font face="Times New Roman, Times, serif" size="5"><b>Is the U.S. going after Syria next?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 18, 2005 | Page 5</font><p>

<font face="Times New Roman, Times, serif" size="3">AFTER IT became clear that the U.S. was losing the Vietnam War, the ruling class responded in different ways.<p>
One section, coming mostly from the Democratic Party that had started the war, began putting forward various "exit strategies" and "peace plans." The Republican Nixon administration tried instead to put down what amounted to a "double or nothing" bet--expanding the war into Cambodia and Laos in the hopes that this would break the stalemate in Vietnam.<p>
It's worth remembering this history as we observe the Bush administration's attempt to ratchet up pressure on Syria.<p>
This pressure gained steam with the release in October of a United Nations (UN) report implicating Syria in the assassination of former Lebanese Prime Minister Rafik Hariri in Beirut earlier this year. Syria may well be the most likely suspect in Hariri's murder, but we should be skeptical of the media echo chamber that has already pronounced it guilty.<p>
The UN report fingering Syria for the Hariri assassination fails to answer many important questions, according to independent journalist Robert Parry of Consortiumnews.com. Parry noted two major issues that should raise questions: the origin of the truck bomb used in the assassination, and the reliability of the major witness implicating Syria.<p>
The UN-led investigation established that the truck used to kill Hariri had been stolen in Japan four months before. This would seem to be a major piece of the case, yet the report seems almost uninterested in trying to explain how a truck stolen in Japan found its way to the Middle East, according to Parry.<p>
What's more, much of the information implicating Syria comes from witnesses of questionable reliability. The testimony of one who claims to have been present in a Syrian intelligence camp where the bomb was loaded onto the truck was introduced to UN investigators by Rifaat al-Assad, the uncle and exiled opponent of the current Syrian president. This witness is also a convicted swindler, according to the German newspaper <i>Der Spiegel</i>.<p>
Given these issues, the case against Syria looks to be less open and shut--and more of a barely competent frame-up.<p>
If all this has a familiar ring to it, it's because the campaign against Syria seems to be following the playbook used during the buildup to the war against Iraq. All of the familiar trappings of preparations for hostile action appear to be taking shape. <p>
There's the 2003 Syrian and Lebanon Accountability Act, a law aimed at isolating Syria politically and damaging it economically. There's even a ready-made "opposition" in the form of the Reform Party of Syria, which definitely has more support in the halls of the American Enterprise Institute and the studios of Washington TV talk shows than it does in Syria. And the Pentagon increasingly accuses Syria of providing a safe haven for "foreign fighters" who enter Iraq to join the resistance.<p>
The U.S. military has already conducted several operations on the Syria-Iraq border to "root out foreign fighters." The possibility of a border skirmish turning into an invasion or an attempt at "regime change" in Syria can't be ruled out--for at least three reasons.<p>
First, Syria is "low-hanging fruit"--a weak and internationally isolated regime that would likely crack as easily as Saddam Hussein's regime did in Iraq. Second, in the strategic vision of the influential administration neo-conservatives aligned with the Likud Party in Israel, regime change in Syria is part of a grand plan to assure U.S.-Israeli hegemony in the Middle East for the foreseeable future. Third, pushing back Syria would be another way of isolating the real stumbling block to total U.S. control of the region--Syria's ally Iran.<p>
The U.S. hopes to use the leverage of the UN report to further isolate Syria. But having shot its international credibility in the invasion of Iraq and tied up its forces fighting the Iraqi resistance, the U.S. may not be able to take military action against Syria, even if it wanted to.<p>

