<font face="Times New Roman, Times, serif" size="5"><b>St. Patrick's Four on trial for opposing Iraq war</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Brian Kwoba</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 16, 2005 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">ITHACA, N.Y.--Peter De Mott, Clare Grady, Teresa Grady and Daniel Burns are being prosecuted by the federal government for the "crime" of resisting the invasion of Iraq. <p>
On March 17, 2003, the group of four <i>Catholic Worker </i>activists from Ithaca, N.Y., entered a military recruiting station to carry out a nonviolent direct action against the looming invasion of Iraq. They poured blood on the walls, the entry hall and the recruiting center's American flag, and then gave a statement which read, in part: "As our nation prepares to escalate a war on the people of Iraq...we mark this recruiting office with our own blood to remind ourselves and others of the cost in human life of our government's war making." <p>
In April 2004, the four activists were tried on charges of criminal mischief and trespassing. By putting the war on trial and arguing that their actions were an attempt to stop a major criminal injustice, nine of 12 jurors voted to acquit them. <p>
Now, the U.S. government is retrying them for using "force, threat, or intimidation" in a "conspiracy to impede an officer of the government," among other charges. What is most alarming, Teresa Grady told <i>Socialist Worker</i>, is that the charge "can be applied almost indiscriminately to anybody who engages in civil resistance and speaks out against the war." <p>
Clearly, the feds are trying to make an example out of the St. Patrick's Four. Explaining his resistance, Danny Burns pointed out that military recruiters "prey on our children and on our poor, on our disenfranchised and on our weak." <p>
"It's about money, it's about profiteering, and it's not the public who are benefiting from this [war]," said Teresa. "In fact, we're paying...corporations to make money so that our young can be sent off into the slaughter."<p>
Judge Thomas J. McAvoy has tried to keep politics out of the courtroom by formally forbidding the defendants from discussing international law, the political context of their actions and other "irrelevant" issues. If convicted, they face up to six years in prison and $250,000 in fines. <p>
Despite this, they remain outspoken in their opposition to the war. "I wouldn't see in jail a spark of the danger that any of the soldiers do," Danny explained. "I wouldn't feel a spark of the fear that some of those children's families in Iraq feel where the families are left behind without their fathers or brothers over there. For me to go to jail would be not even close to what these people are experiencing while Bush's daughters and himself are on vacation." <p>
Accompanying the trial will be a "Citizens' Tribunal on the War in Iraq," featuring prominent activists such as Code Pink founder Medea Benjamin, war resister Camilo Mej&iacute;a and antiwar Iraq veteran Jimmy Massey, as well as other leading antiwar voices.<p>
