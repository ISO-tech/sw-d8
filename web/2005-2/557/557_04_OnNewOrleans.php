<font face="Times New Roman, Times, serif" size="4">George Galloway's statement on the disaster on the Gulf Coast</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"The lives that Bush holds cheap"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 16, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>GEORGE GALLOWAY released the following statement on the disaster on the Gulf Coast, on the eve of his North American <a href="http://mrgallowaygoestowashington.com" target="_blank">antiwar speaking tour</a>.</b><p>

THE SCENES from the stricken city almost defy belief. Many, many thousands of people left to die in what is the richest, most powerful country on earth. <p>
This obscenity is as far from a natural disaster as George Bush and the U.S. elite are from the suffering masses of New Orleans. The images of Bush luxuriating at his ranch and of his secretary of state shopping for $7,000 shoes while disaster swamped the U.S. Gulf Coast will haunt this administration.<p>
In the most terrible way imaginable, they show to the whole world that it is not only the lives of people in Baghdad, Falluja and Palestine that Bush holds cheap. It is also his own citizens--the Black and poor people left behind with no food, water or shelter.<p>
This is not simply manslaughter through incompetence, though the White House's incompetence abounds. It is murder--for Bush was warned four years ago of the threat to New Orleans, as surely as he was warned of the disaster that would come of his war on Iraq. <p>
Yet he plundered the city's defenses to fill the maw of his war machine, and the body count rises at home and abroad. He has ravaged what little U.S. welfare provision there was to such an extent that virtually the only public institution left is the Pentagon's repressive apparatus, deployed against the people of Iraq and now, as sickeningly, against his own citizens scavenging for fresh water.<p>
His is the America of Halliburton, the M-16 rifle, the cluster bomb, the gated communities of the rich and the billionaires he grew up with in Texas. <p>
There is another America. It is the land of the poor of Louisiana, it is the land of the young men and women economically conscripted into the military. It is the land of the glorious multiethnic mix that was New Orleans--it is the land of Malcolm X, Martin Luther King and of great struggles for justice. <p>
It is to that America that I am travelling next week to begin a national speaking tour. It is the people of that America, the majority, who will take to the streets of Washington on September 24 to march against the war on Iraq--and, we must now add, against the war of rich America against its own citizens.<p>
