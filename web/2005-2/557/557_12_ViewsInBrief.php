<p><a name="Poor"></a><font face="Times New Roman, Times, serif" size="4"><b>Poor pitted against the poor</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I WOULD like to thank <i>Socialist Worker</i> for its excellent coverage of the unnatural horrors produced in the wake of Hurricane Katrina. However, I want to address a related issue that has been raised here in New Haven, as I think it is incredibly important.<p>
New Haven, Conn., has now pledged to house up to 100 families who have been left homeless due to Hurricane Katrina. The estimated yearly cost of providing housing, schooling and job training is $80,000 per family, or $8 million overall. Those who arrive in New Haven also can expect help with drivers' licenses, Social Security cards and other key paperwork. Lawyers will help the newcomers with insurance and other needs. <p>
The Housing Authority has arranged for 19 apartments in three high-rises and is asking the U.S. Department of Housing and Urban Development for 100 new Section 8 vouchers to deal with evacuees' long-term needs.<p>
While providing needed assistance to these families is certainly welcome, the city has chosen to do so at the expense of New Haven's own homeless population. <p>
There are more than 4,700 homeless people in the New Haven area, 20 percent of them spent at least one year on the streets. People often remain on the waiting list for subsidized housing for years. <p>
At present, approximately 1,800 New Haven families are on waiting lists to be placed in public apartments that will now go to refugees. Richard Knight told the <i>Boston Globe</i> that he lives in a homeless shelter as he waits for available housing. "I feel sorry for those people, but people on the housing authority list are on hold," he said.<p>
The needs of those in New Orleans should not be counterposed to the needs of those in New Haven and across the country. Rather, the tragedy in the Gulf has merely exposed a system that has long been cruel and unjust to the poor on a daily basis. <p>
New Haven Mayor John DeStefano exposed his hypocrisy when he said, "This isn't about the families; this is about our community's character, our community's willingness to sacrifice. This is an opportunity to treat others as we would like ours to be treated." For the homeless in New Haven, many of whom have suffered and died for want of shelter, this must come as a slap in the face.<p>
In New Haven, we held a protest downtown on September 7, where we chanted "Food and shelter for those in need--from New Haven to New Orleans!" In our public outcry over the inhumane response to Hurricane Katrina, we should never forget the inhumanity produced by the capitalist system every single day.<br>
<b>Leela Yellesetty</b>, New Haven, Conn.<p>
