<font face="Times New Roman, Times, serif" size="4"><b>Unfit to be the ruling class</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THE PICTURES and stories from New Orleans almost defy belief. Even with several days' warning, the authorities failed to provide tens of thousands of people--mostly, of course, African American--with any way of leaving before Hurricane Katrina struck. After disaster had engulfed the city, it took several more days before the most basic emergency relief arrived.<p>
Instead, the Bush administration blamed the victims of this catastrophe for staying, even though most had no way to leave, and then announced a shoot-to-kill policy against "looters" desperately seeking food and water to survive. This response goes beyond mere incompetence and indifference--it is criminal to the highest degree.<p>
In the <i>Communist Manifesto</i>, Marx and Engels wrote: "The bourgeoisie is unfit any longer to be the ruling class in society, and to impose its conditions of existence upon society as an overriding law. It is unfit to rule because it is incompetent to assure an existence to its slave within his slavery, because it cannot help letting him sink into such a state, that it has to feed him, instead of being fed by him."<p>
The U.S. ruling class is worse than unfit. It is responsible for the deaths of 10,000 people in Louisiana and proved incapable of even feeding the survivors. It's past time to hold these criminals accountable for their actions.<br>
<b>Phil Gasper,</b> Berkeley, Calif.<p>
