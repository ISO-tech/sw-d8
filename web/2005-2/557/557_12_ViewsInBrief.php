<p><a name="Proposal"></a><font face="Times New Roman, Times, serif" size="4"><b>A proposal for public housing</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IN THE face of the massive catastrophe on the Gulf Coast, I was shocked by the poor taste of the headline on the real estate section in the September 4 <i>Providence Journal:</i> "For sale: Single family, 100 rooms."<p>
I'm certain there are hundreds of families who would love to have one room of their own, be it in the Vanderbilt summer mansion in the Berkshires advertised in the article, or anywhere else that's safe. Given the pathetic response of George Bush and the federal government, the least they can do is to buy that $21.5 million monster and give it over to the refugees of New Orleans as free, permanent housing. <p>
Preference for housing in the swankest refugee camp ever should naturally go to the poor and Black, as they are the ones who had substandard housing on a good day before the storm, who could not evacuate a city whose plan assumed everyone had a car, and who are now bearing the brunt of the racist "shoot-to-kill" orders of the Louisiana governor. <p>
Anything less than this simply continues the daily racism and oppression people suffered before, which has been so clearly exposed by this tragedy. <br>
<b>Brian Chidester</b>, Warren, R.I.<p>

