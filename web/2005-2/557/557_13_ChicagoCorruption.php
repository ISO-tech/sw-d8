<font face="Times New Roman, Times, serif" size="5"><b>Corruption still king in Chicago</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Joe Allen</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 16, 2005 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Robert Cooley with Hillel Levin, <b><i>When Corruption Was King: How I Helped the Mob Rule Chicago, Then Brought the Outfit Down. </b></i>Carroll &amp; Graf, 2004, 368 pages, $26.</font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO MAY be the third-largest city in the U.S. and the "Second City" when it comes to culture, but without a doubt, Chicago is America's first city when it comes to political corruption.<p>
Not a day has passed in the last five months without the media exposing some lurid story of city hall corruption. The current ruler of Chicago, Mayor Richard Daley II--who just a short time ago was considered by <i>Time </i>magazine to be one of the best mayors in the country--suffered the humiliation of being interrogated by the U.S. Attorney's office about the recent scandals.<p>
The recent revelations of corruption in Chicago politics may come as no shock in the city identified around the world with Al Capone. Yet what has been hidden is how the corruption was organized by the "City's Fathers"--the mobsters, the politicians and the businessmen.<p>
Robert Cooley's <i>When Corruption Was King </i>is an insider's account of how the mob and their Democratic Party allies ruled the "city that works" in the 1970s and '80s--especially how they "fixed" the judiciary. Cooley, the son and grandson of Chicago police officers, learned quickly that if he wanted to be a successful lawyer he had to deal with the Democratic Party's First Ward leader Pat Marcy. <p>
The First Ward was a sprawling district that went from Chicago "Gold Coast" on the North Side to the steel mills on the South Side. It was the major institution through which the mob, businessmen and the Democrats made deals and swapped cash. "The First Ward...was about power, and that was the one thing I couldn't buy," recounts Cooley. "I knew how to work the system; these guys controlled the system. They could hire and fire cops. They could even make or break judges."<p>
Cooley's big break came when Marcy approached him to "fix" a murder case involving notorious mob assassin Harry Aleman. Cooley bribed Judge Frank Wilson--known as a "law-and-order" judge--to acquit the murderer for a measly $10,000.<p>
This opened a floodgate of business for Cooley. Cooley extensively recounts his "highlife among the lowlifes" of the Chicago underworld--the book reading like <i>Goodfellas</i> with a law degree.<p>
In the 1980s, Cooley's life as a "fixer" became unsustainable. In 1986, the FBI approached him, and he wore a wire for the next couple years. In a series of trials that began in 1990s, Cooley helped put 24 people in prison, including two judges.<p>
The most notorious judge Cooley helped put away was Thomas J. Maloney, who is responsible for putting large numbers of people on death row for crimes they didn't commit. Some, but not all, of Maloney's victims have since received pardons or new trials. Maloney was sentenced to 15 years in prison.<p>
The current Mayor Daley came to power as Cooley's testimony was tearing down the old First Ward system. When Daley was Cook County State's Attorney in the 1980s, he neither heard the screams of the torture victims in his jails nor saw the rampant corruption in the judiciary.<p>
Daley reorganized Chicago corruption over the last decade and a half with an array of new institutions. Cooley's book and the recent revelations about the Daley administration shows us how corruption and city politics go hand-in-hand.<p>

