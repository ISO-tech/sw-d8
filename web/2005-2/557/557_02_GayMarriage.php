<font face="Times New Roman, Times, serif" size="4">Schwarzenegger vetoes landmark gay-marriage law</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Equal rights denied</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Matt Swagler</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 16, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">CALIFORNIA GOV. Arnold Schwarzenegger will veto landmark legislation that would legalize same-sex marriage.<p>
The State Assembly narrowly passed the legislation--the first time ever that a legislature has approved a bill that would legalize equal marriage rights for gay and lesbian couples. But within hours of gay-rights supporters celebrating this victory, Schwarzenegger announced that he would veto the bill--denying the widespread support in California for equal marriage rights.<p>
Schwarzenegger hypocritically prefaced his decision by claiming his support for civil rights and equality for gay couples. Just before announcing that the governor would veto the bill, Schwarzenegger's press secretary stated that the governor "considered no undertaking to be more noble than the cause of civil rights. He believes that gay couples are entitled to full protection under the law and should not be discriminated against based upon their relationship."<p>
The governor claims that gays and lesbians have "full protection under the law" because of California's current domestic partner rights. But gay-rights activists across the country have challenge this "separate but equal" second-class status.<p>
Schwarzenegger justified his veto decision by pointing to a state referendum passed in 2000, under which only marriages between a man and women can be recognized in California. The governor claims this made the legislature's bill "unconstitutional," and only a court decision or another referendum can legalize same-sex marriages.<p>
But this is only a cover for Schwarzenegger, who has carried out a right-wing offensive since taking office. Californians have faced cuts in funding for education, pensions and social services.<p>
But Schwarzenegger's attacks have infuriated millions, and his approval rating has dropped to an all-time low of 31 percent. Meanwhile, support for gay-marriage rights continues to grow, according to a recent poll by the Public Policy Institute. This support was reflected in the passage of the same-sex marriage bill, which had previously been voted down by the Democratic Party-dominated legislature.<p>
While organizations such as the United Farm Workers recently came out in support of same-sex marriage rights, the movement has remained largely quiet and off the streets in California since the 2004 elections, when gay-marriage advocates were chastised by Democrats like Sen. Diane Feinstein for demanding "too much, too fast, too soon." <p>
A visible and unapologetic movement can add to the growing support for equal marriage rights. Gay-marriage activists and supporters should take this opportunity to organize a mass movement that will force the legislature, the courts and Schwarzenegger himself to grant us our rights.<p>

