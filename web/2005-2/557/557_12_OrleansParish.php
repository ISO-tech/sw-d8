<font face="Times New Roman, Times, serif" size="5"><b>Death of Orleans Parish?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 16, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">HAS THERE been a week when so many in the U.S. have had the worst week of their lives? I grieve for the people of the entire Gulf Coast region, but focus here on Orleans Parish because, in my opinion, it has been attacked, and hundreds if not thousands have been killed, by the actions and inactions of government.<p>
Sitting here, I can't sort out the relative failures of the city, state and federal governments. All levels of government knew that this type of catastrophe could happen--they gamed for it in their "Hurricane Pam" scenario. They knew that a Category 4 hurricane would swamp the levees and drown the city.<p>
All levels of government knew that this particular "perfect storm" (coined thus by Homeland Security Director Michael Chertoff as an excuse for government failures) was coming and that everyone who stayed faced a high risk of death. Yet there were no mandatory (I mean really mandatory) evacuations. <p>
This was obviously a storm that would strike the entire Gulf region and affect the entire nation. Yet the Bush Administration did not take leadership. Instead, the government merely reacted after the fact.<p>
Outrageously, authorities directed many thousands to the Superdome, the Convention Center and other "shelters" with no food or water, no lights, no sanitation and no security. People got to experience hell on earth, courtesy of the government, which enticed and abandoned them.<p>
Perhaps Bush can't be blamed for the start of the shelter fiascos. Yet while he was cavorting in Arizona and San Diego, the city was drowning and people were falling into an abyss. <p>
Was it all merely incompetence? I would like to think so, but I am not convinced. A few facts: Orleans Parish is overwhelmingly African American (67 percent) and overwhelmingly Democratic (it voted for Kerry by more than three to one). Those left behind in the squalor of the shelters were nearly all Black and too poor to have been able to afford an escape from town by private automobile. <p>
The biggest group of white faces was seen at the Superdome when hundreds of tourists were able to cut in front of thousands of poor people who had waited for days in the muck and mire. The privileged guests from a nearly hotel boarded buses brought especially for them. It was mind-numbing and mind-boggling for storm victims and reporters who watched this spectacle.<p>
While the rich got a quick ride out of town, the masses could only wait during the day, hoping to survive the heat and lack of water. During the night, they huddled in fear of attack after the police and National Guard had gone off shift, leaving armed gangs to roam and terrorize. <p>
This sounds like a plot for an early Mel Gibson movie. No one would have believed that such a thing could happen, not in the United States of America. Now everyone believes that this can happen here, because it has. Our views of ourselves--what were are capable and incapable of--are changed forever.<p>
I consider some important facts--the forewarnings not heeded, the opportunities for preventive and remedial measures not taken, and the abandonment of people who had already been shuffled aside by the economic system. I have concluded that the government has demonstrated its contempt for these people based on their race and class. Maybe I'm wrong. Maybe it was just over-the-top, unbelievable incompetence. We'll see what happens.<p>
Will the Orleans Parish victims be restored to their homes? Will the government fund their rebuilding? Will reconstruction jobs go to local inhabitants? Will the flood-control system finally get the funding it has needed for years? Will the schools and community institutions be rebuilt to meet the needs of the people who have lived there for generations?<p>
Or will the victims (termed "refugees" by a government intent on moving them) be permanently resettled in scattered locations across the nation? Will Halliburton and their ilk get the reconstruction dollars and bring in outsiders to rebuild? Will landowners take their insurance monies and run? Will poor landowners be offered pennies on the dollar to sell their soggy bottomland, or will land be taken by eminent domain? Will New Orleans be rebuilt as a glitzier version of itself as "tourist city," with a new set of low-wage workers--but without the community ties--imported to replace the dispossessed natives of the city?<br>
<b>Charlie Jenks,</b> Traprock Peace Center, Deerfield, Mass.<p>

