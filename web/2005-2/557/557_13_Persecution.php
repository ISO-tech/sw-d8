<font face="Times New Roman, Times, serif" size="4">Stories of persecution after September 11</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Every immigrant is now a suspect</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Manijeh Moradian</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 16, 2005 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Tram Nguyen, <b><i>We Are All Suspects Now: Untold Stories from Immigrant Communities after 9/11. </b></i>Foreward by Edwidge Danticat. Beacon Press, 2005, 208 pages, $14.</font><p>

<font face="Times New Roman, Times, serif" size="3">TRAM NGUYEN'S <i>We Are All Suspects Now: Untold Stories from Immigrant Communities after 9/11</i> exposes the brutality and racism of the domestic "war on terror" on some of the most vulnerable people in U.S. society.<p>
The scale of the attacks is enormous, from the 10,000 people who have been detained and frequently tortured in the Northeast alone, to the more than 1,000 hate crimes against Arabs and Muslims, to the thousands who have been deported--often back to the very life-threatening conditions from which they fled in the first place. <p>
This book provides the harrowing details of lives turned upside down--or, in some cases, destroyed completely--through the personal testimonies from individual Pakistanis, Haitians, Mexicans and Somalis who have been targeted since September 11.<p>
A Somali man caught up in the Islamaphobic witch-hunts compares his experience in the U.S. to the refugee camps in Kenya from which he escaped. "There were flies everywhere," Abdullah Osman said of the county jail in Oklahoma where he was detained for three months out of his nearly two-year imprisonment. "There were 75 or 80 people in one room. It was worse than any refugee camp. I saw people vomit blood."<p>
With chapters on Muslim communities in Brooklyn and Minneapolis, as well as one called "Crisis at the Border" about growing vigilante violence in the Southwest, Nguyen shows how anti-terrorist hysteria has affected all immigrants.<p>
In a chapter called "The New Racial Profiling," Nguyen discusses the way racism pits oppressed groups against each other with disastrous effects. He notes that a poll taken immediately after September 11 showed 71 percent of Blacks in favor of profiling Arabs while at the same time "the number of Black men in prison now matches the number of men enslaved before the Civil War."<p>
The scapegoating of immigrants in times of war is nothing new, and Nguyen sees the current round-ups as part of a long American tradition that includes the anti-immigrant Palmer Raids of 1919 and the internment of Japanese Americans in concentration camps during the Second World War.<p>
He also points out that the laws requiring mandatory detention for immigration violations, as well as many other provisions that make today's xenophobic crackdown possible, were put in place under Democratic President Bill Clinton. He argues that the destruction of welfare, the strengthening of the criminal justice system and anti-immigrant legislation pushed through during the 1990s set the stage for the Immigration and Naturalization Service, the FBI and now the Department of Homeland Security to wreak havoc on the lives of tens of thousands of innocent people, who suddenly became suspects simply because of their race or religion after September 11.<p>
Among the shattered dreams of so many immigrant families who came to this country to find a better life than the one they left behind, there is also cause for hope among the many voices included in this book. The profiles of activists like Bobby Khan of the Coney Island Avenue Project and Aarti Shahani of Families for Peace show that, even while their families and communities are under siege, it is possible for those targeted to fight back.<p>

