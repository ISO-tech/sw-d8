<font face="Times New Roman, Times, serif" size="5"><b>Rising poverty in Bush's America</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 16, 2005 | Pages 1 and 2</font><p>

<font face="Times New Roman, Times, serif" size="3">FOR MILLIONS of Americans, Hurricane Katrina revealed the deadly impact of class and racial inequality in the richest country on earth. Even the mainstream media couldn't ignore the fact that the hurricane's biggest victims were the poor, who lacked the resources to escape the deadly storm.<p>
But the Gulf Coast region is hardly the only place in the country where poverty afflicts large numbers of people.<p>
In fact, the day after Katrina struck, the U.S. Census Bureau announced that the number of Americans living in poverty had increased for the fourth straight year. Despite the fact that the U.S. economy is in the fourth year of an economic recovery from the 2001 recession, the percentage of Americans living in poverty increased to 12.7 percent last year from 12.5 percent in 2003.<p>
At the same time, another 800,000 people lost access to health insurance, pushing the number of uninsured Americans up to an all-time high of 45.8 million people. Median household income remained unchanged from 2003--a slight improvement on previous years, when it declined.<p>
The deadly impact of these statistics could be seen most starkly in New Orleans, where the rich evacuated their mansions in private helicopters while the poor were forced to spend days without food or water in the hellholes of the Superdome and the New Orleans Convention Center.<p>
While Katrina revealed the most extreme results of this polarization of rich and poor, it's clear that slow-motion disasters are unfolding in every American city and state. In Manhattan, the gap between the richest and the poorest one-fifths of the population now matches the gap in Namibia, according to a <i>New York Times </i>study.<p>
Comparing parts of the U.S. to a Third World country isn't simply a shock tactic. The widely respected United Nations <i>Human Development Report</i>, also released the week Katrina hit the Gulf Coast, showed that the U.S. matches Malaysia in the rate of children who die before their first birthday.<p>
For most of the 20th century, the U.S. infant mortality rate declined. But since 2000, it has increased. Black children living in Washington, D.C., die at the same rate as children in the Indian state of Kerala. More than 20 percent of U.S. children live in poverty, a milestone that the U.S. shares with Mexico.<p>
The situation has grown so severe that the normally reserved UN accused the U.S. of having "an overdeveloped military strategy and an underdeveloped strategy for human security."<p>
All of this comes as the U.S. economy added 2.2 million jobs last year, after three years of job losses. "The gains are going all to the top," said sociologist Andrew Beveridge, who conducted the research for the <i>Times</i>. "It's a massive class disparity."<p>
Like the New Orleans rich with their heliports, billionaire real estate developer Donald Trump doesn't notice a gap between the rich and poor. "Times have been good, but times have been good for many people and many classes of people," Trump told the <i>Times</i>. "I think there is a very large middle class--but not in this section, by the way."<p>
Meanwhile, in the Wagner Houses, a public housing project only a few miles from Trump Tower, auto mechanic Darryl Powell told the <i>Times </i>that his neighbors were struggling. "They're trying to keep a roof over their head," he said. "People are trying to hold onto what they get." Sheila Estep faces eviction from the Wagner Houses because she was working full-time at home trying to raise her kids, instead of in her earlier jobs in the building trades.<p>
If the worst happens, Sheila and her three kids will end up like hundreds of thousands in the Gulf region--homeless. And she won't be a victim of a hurricane and flood. Instead, she'll be a victim of a system that rewards a tiny minority with more wealth than it can possibly use--while millions more struggle just to keep from sinking deeper into poverty.<p>

