<font face="Times New Roman, Times, serif" size="4">Puerto Rico teachers' union fends off takeover bid</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>AFT maneuver defeated</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 16, 2005 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>ROBERTO BARRETO writes from Puerto Rico on a battle for union democracy.</b> <p>

SAN JUAN--The largest union in Puerto Rico has won a key battle in court against the attempted takeover by its former parent union in the U.S. The American Federation of Teachers (AFT) failed in its bid to take control of the Puerto Rican Teachers' Federation (FMPR) after losing a ruling in the U.S. District Court here.<p>
In September 2004, the FMPR's assembly of delegates voted democratically to disaffiliate from the AFT, declaring their independence. The AFT refused to recognize this decision and tried to seize control of the FMPR.<p>
But an August 16 decision by Federal Judge Jay Garc&iacute;a Gregory found that U.S. laws couldn't be used to put a Puerto Rican union under administration, or trusteeship. Then, on August 29, FMPR members voted to confirm their disaffiliation from the AFT by a total of 19,421 to 5,882.<p>
The FMPR, which represents some 43,000 education workers, had been affiliated with the AFT since 1965 without receiving many benefits for Puerto Rican teachers, despite paying $2.6 million in dues every year. What's more, the AFT supported the anti-worker Law 45 that allows the government to limit and criminalize basic trade union rights for public employees.<p>
In 2003, a new FMPR leadership, running on a militant platform of struggle against Law 45 and disaffiliation from the AFT, won union elections. The AFT responded by trying to impose a trusteeship over the FMPR, replacing the democratically elected leadership with one that would do its bidding. <p>
The AFT also boycotted contract negotiations but kept the FMPR's dues money flowing into its coffers. They betrayed not only the teachers but all the workers, since results of the FMPR's contract negotiations have repercussions for the entire working class in Puerto Rico.<p>
After the AFT announced that it would take control of the FMPR, hundreds of teachers picketed the federal court and AFT offices in San Juan. Teachers surrounded the FMPR's headquarters building, pledging to resist any attempt to seize their union. <p>
By trying to impose a trusteeship through the federal district court, the AFT showed that it has no respect for the self-determination of Puerto Rican workers. To carry out its dirty work, the AFT leadership in Washington tried to use the same federal court that has implemented U.S. colonialism--from imprisoning pro-independence leaders to, most recently, prosecuting hundreds of activists in the movement to drive the U.S. Navy out of Vieques.<p>
The struggle over the FMPR is likely to continue, as the AFT is expected to appeal the court decision. The FMPR is ready to fight and is asking for solidarity from across the labor movement. <p>
Workers in the U.S. should oppose the AFT's case against the FMPR and respect the right of Puerto Rican workers to decide their own fate. We must further condemn the collaboration between the AFT leadership and the repressive institutions of U.S. colonialism. The entire labor movement in the U.S., Puerto Rico and the rest of the world should repudiate the AFT bureaucrats.<p>
