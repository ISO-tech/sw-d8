<font face="Times New Roman, Times, serif" size="5"><b>Ohio restriction halts abortions</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 16, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">WOMEN'S RIGHT to abortion came to a halt in Ohio September 9 after a seven-year-old law went into effect requiring that women seeking to terminate a pregnancy meet face-to-face with a doctor at least 24 hours beforehand. A 16-year-old rape victim who had traveled from Akron to Cleveland on a Greyhound with her mother had to be turned away from a clinic, reported the <i>Cleveland Plain Dealer</i>.<p>
On September 8, a federal judge in Cincinnati ruled in favor of upholding the law, which also requires unmarried minors who want abortions to get parental consent. <p>
The 1998 law changed existing parental notification laws that allowed the woman seeking an abortion to seek judicial bypass if she could convince a judge that the abortion was in her best interest, that she was mature enough to make the decision, or that she would face abuse by telling her parents. The new law still allows a woman to seek a judge's permission to bypass parental consent, but she can no longer use potential abuse as a defense. <p>
Ohio's Republican Attorney General Jim Petro called the ruling a "victory for the unborn."<p>
Abortion providers rushed to cancel appointments and break the news to frantic patients. Lawyers who had challenged the constitutionality of the law before went back to court and received a temporary, two-week delay. Clinics now have until September 22 to comply.<p>
"She was scared when she heard about the new law," said Linda Jane of Preterm in Cleveland, the clinic where the 16-year-old rape victim was turned away. "She wants to go to college and do something with her life. She'll have to go without anesthesia tomorrow because we don't offer it on Saturdays."<p>
Carol Westfall, executive director of Cleveland Surgi-Center Inc., told the <i>Plain Dealer, </i> "We had people in here sobbing who had taken days off work but couldn't take more days off work. These women have thought pretty long and hard about their decision, and they're pretty clear about what they're going to do."<p>
With congressional Democrats set to give Supreme Court nominee John Roberts a free ride on the question of abortion, this is one more example of how the right is waging a war on women's access to abortion--one procedure and one state at a time.<p>

