<font face="Times New Roman, Times, serif" size="5"><b>Challenging the bigots at the border</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Avery Wear</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 16, 2005 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">ACTIVISTS ACROSS the Southwest are planning to make the trip to the remote desert border town of Calexico, Calif., on September 17. They plan to protest and disrupt a vigilante anti-immigrant group, Friends of the Border Patrol (FOB), when it mobilizes to the border.<p>
The counterprotesters' border-spanning rally, planned together with residents of the Mexican city of Mexicali, promises to be a highlight of the weekend. And in marked contrast to earlier anti-vigilante protests, many local residents, including Calexico Mayor Alex Perrone, are organizing together with pro-immigrant rights protesters. <p>
The FOB--inspired by the Minuteman Project that descended on the Arizona border earlier this year--is vowing to stretch 100 volunteers across California's southern boundary to catch undocumented border-crossers. <p>
This couldn't come at a worse time. The death toll for those crossing has set a new record this year, and passed the 3,300 mark overall since the toughened border control measures of Operation Gatekeeper were imposed in 1994. Vigilantes will force migrants to cross in riskier and more rugged areas, to play hide and seek with them, or, if they are caught, to try crossing again later, all of which increases their chance of dying. <p>
Millions of undocumented workers do the most dangerous, dirtiest and worst-paid work in the U.S., producing enormous profits for business interests and paying much more into state tax systems and Social Security than they receive. Yet they are scapegoated as "illegals" who "steal jobs" and "drain social services."<p>
The FOB's projection of 100 vigilantes is down sharply from similar efforts earlier this year, which themselves turned out fewer people than advance predictions. James Gilchrist's Minuteman Project vowed to mobilize 1,000 volunteers for Arizona--it got fewer than half that. <p>
In California, James Chase and his Border Patrol Auxiliary produced only a few dozen people for its July patrol--with only Chase himself remaining by the final day. The California bigots were confronted by furious counterprotesters, estimated to outnumber the vigilantes by 7 to 1, according to the <i>San Francisco Chronicle</i>. These activists forced the vigilantes to answer to charges of racism that they had carefully avoided before. <p>
Immigrant-rights protesters now believe that the vigilante movement may be demoralized. The broader anti-immigrant movement sparked by the Minutemen, however, continues to gain ground, as politicians follow the course charted by Gilchrist. <p>
"Liberal" Latino State Assembly Speaker Fabian Nu&ntilde;ez of Los Angeles became the latest Democrat to call for California to declare a state of emergency about immigration last month--something that even Minuteman-praising Republican Gov. Arnold Schwarzenegger resisted. Far-right Republicans like Colorado Rep. Tom Tancredo have gained national support, and Gilchrist now plans to run for Congress. <p>
Even if we stop the Minutemen in Calexico, the challenge of building a truly mass movement to counter the racists remains.<p>

