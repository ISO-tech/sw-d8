<font face="Times New Roman, Times, serif" size="4">Texas plans to kill Frances Newton</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Stop this execution!</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lily Hughes</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 9, 2005 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">THE STATE of Texas wants to execute Frances Newton on September 14. If they do, she will be the first Black women executed in the state since the Civil War. <p>
Frances has steadfastly maintained her innocence in the killing of her husband and two children in Houston in 1987. In her most recent appeals, Frances' lawyers have highlighted many serious problems with the case against her, including recent disclosures by prosecutors that call into question ballistic evidence supposedly linking Frances to the crime. <p>
Ballistic testing in the case was done by Houston's Harris County crime lab, now under federal investigation for gross incompetence, faulty testimony in trials and mishandling of evidence. New testing matches the bullets to the gun prosecutors say was used in the murder. <p>
However, new evidence has come to light about a second pistol recovered by police the night of the murder. Frances says that the officer who arrested her told her that her gun didn't match the murder weapon. But no mention was made again of a second gun until a prosecutor accidentally spoke about it in a recent interview--casting further doubt on the validity of the ballistic evidence.<p>
The victims were all shot at close range, and testing found no evidence of blood on the gun or Frances clothes, and no gunpowder burns on her hands. A skirt that Frances was wearing was found to have nitrate stains on it--she says this likely came from garden fertilizer, and the prosecution claimed it was gunshot residue. New technology would have determined the nature of the residue, but initial testing destroyed the evidence. The skirt was then stored with other evidence from the crime, contaminating it completely.<p>
Frances has said from the beginning that she believed the murder was committed by a drug dealer that her husband owed money to. Her attorneys have also shown that there would have been no time for Frances to have cleaned up the scene before calling police.<p>
Newton's original defense attorney was none other than Ron Mock, who has been sanctioned by the state bar association at least three times and is no longer allowed to work on death penalty cases. He also represented Shaka Sankofa (also known as Gary Graham), an innocent man put to death by Texas in 2000. Mock called no witnesses and did no investigation in Frances' case, telling the court at the time, "I'm a lawyer, not an investigator."<p>
This planned execution comes on the heels of increased scrutiny of the Texas death penalty system by the U.S. Supreme Court--which rebuked both the state's Court of Criminal Appeals and the federal 5th U.S. Circuit Court for their handling of appeals in capital cases in Texas. The courts have voted to deny relief in the overwhelming majority of death penalty appeals. Texas has the lowest reversal rate of any state. Both the Texas high court and the 5th Circuit turned down Frances' appeal.<p>
But the media spotlight on the Houston crime lab scandal as well as other high-profile death cases in Texas is beginning to take a toll. The number of death sentences being pursued by prosecutors is steadily declining. <p>
"Just knowing my own innocence, that helps a lot," Frances said in an interview with Dutch  Public Broadcast. "I cope knowing that I have people fighting for me, believing in me, and believing that the right thing will be done."<p>
Those people include Frances' family and the family of her murdered husband, as well as abolitionists in Texas. The Committee to Free Frances Newton (see www.freefrances.com) in Houston has held marches and spearheaded a postcard campaign, as well as producing a DVD of interviews with Frances. <p>
In Austin, the local chapter of the Campaign to End the Death Penalty is hosting Jewel Nelms, Frances' mother, at a panel discussion this week. Campaigners in Austin have gathered hundreds of signatures on petitions for Frances, and and are planning to protest on the night of the scheduled execution. <p>
We can't let the state of Texas execute Frances Newton!<P>

