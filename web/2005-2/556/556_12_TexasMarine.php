<font face="Times New Roman, Times, serif" size="5"><b>Why isn't the war on trial?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 9, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">WHEN THE bloodthirsty district attorney in the east Texas town of Tyler fails to seek the death penalty for an African American man accused of murdering a young white woman in a high-profile case, you can be sure something fishy is going down.<p>
Johnny Lee Williams Jr. was charged with abducting Megan Holden from a Wal-Mart parking lot in Tyler, sexually assaulting and murdering her before stealing her car and fleeing west.<p>
Williams' parents appeared on local media and ABC's <i>Good Morning America </i>to describe how their son had changed after returning from a tour of duty in Iraq. Their account of Williams' behavior detailed classic symptoms of post-traumatic stress disorder. Williams and his parents had sought help from the Marine Corps, to no avail.<p>
The judge in the case issued a gag order on all involved in the case even before Williams was indicted, and District Attorney Matt Bingham began to brag about getting the death penalty. The trial was to begin July 25, but on July 7, Williams pled guilty to all charges against him and was sentenced to five stacked life sentences. He will not be eligible for parole for 160 years. <p>
According to Casey Knaupp of the <i>Tyler Morning Telegraph</i>, Williams "debriefed" members of Holden's family, his family, law enforcement officials and his three court-appointed attorneys "for five hours about what happened during the last day of Ms. Holden's life."<p>
Williams deserved a fair trial, and Bingham knew that Williams' symptoms of post-traumatic stress disorder would have to be addressed. Bingham wanted to make sure that the U.S. Marines and the war in Iraq got off scot-free. "The debriefing also cleared up misconceptions about Williams serving in Iraq," Bingham said, adding that "it was disgraceful for the defendant to blame Iraq for what he did when so many are giving their lives fighting."<p>
But when did Williams blame Iraq? It was Williams' parents who described his symptoms. <p>
Are we to believe that Williams was planning his defense for a crime sometime in the future when he told his dad of being tormented by having killed three people, including a child? When he told his mom that "some of the stuff they make you do is all messed up"? Was Williams faking the flashbacks and depression? Did his mom lie when she told of his having friends over so he could fall asleep at night?<p>
Patricia Williams' accusation that the Marines taught her son to kill, but not how to adjust back into society, deserved a fair hearing. According to the Army's surgeon general, "30 percent of U.S. troops surveyed have developed stress-related mental health problems three to four months after coming home from the Iraq war." The percentage is probably much higher.<p>
According to the <i>Houston Chronicle</i>, Smith County, in which Tyler is the county seat, has a reputation for "its own branch of justice...which is tainted and inequitable." With his five life sentences, Williams is "erased from society," says Bingham. Meanwhile, Johnny Lee Williams sits in a Texas state jail, where he will receive no help for the demons from the Iraq war that torture him.<p>
Bingham's refusal to consider the possibility that the nightmare that is the Iraq war might have played a role in Megan Holden's death is a disservice to Holden's family and the entire community. A few days after District Attorney Bingham let the war off the hook, 27 members of the Army's 141st Quartermaster Group based in Tyler left for Iraq.<br>
<b>Cindy Beringer, </b>Austin, Texas<p>

