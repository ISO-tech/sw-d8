<font face="Times New Roman, Times, serif" size="4">Anti-death penalty activist on bigotry in her hometown</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"The racism I've seen in New Orleans"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 9, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">MONIQUE MATTHEWS is the sister of exonerated death row prisoner Ryan Matthews. Arrested at age 17 for a crime he didn't commit and convicted in a two-day trial riddled with racism, Ryan was finally released from Louisiana's death row in June 2004. Monique and her mother, Pauline, became outspoken fighters for justice on Ryan's behalf and are active in the Campaign to End the Death Penalty. <p>
Monique, Pauline and their family evacuated their New Orleans home on Sunday, the day before Hurricane Katrina struck, and made it safely to Chattanooga, Tenn. Ryan evacuated to Baton Rogue, La. <i>Socialist Worker's </i>LEE WENGRAF spoke to Monique about their ordeal, and about the city's legacy of racism.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>WHAT WAS it like getting out of the city?</b> <p>

WE JUST picked up some clothing and made sure we had a few things, like baby pictures and things that have value. It took us 12 hours to drive out, a drive that should have taken six or seven. On the way out, it was bumper-to-bumper. There were accidents everywhere. There were cars overheating. All the police said was, 'You've got to get out of here.' <p>
But we thought we'd be able to come right back after a few days. We thought it wouldn't be so bad. Now we fear that we won't have anything to go back to. There's the stories of looters and of alligators coming in, plus, we lived by a canal. We're going to have nothing. <p>

<b>WAS THERE any help for people who might not have been able to evacuate on their own?</b> <p>

WE ONLY heard of people being able to go to the Superdome. There were no offers of transportation. They were telling people to provide for themselves. <p>
It's the fault of politicians--they should have prepared people for this. The areas heavily flooded were mostly African American, poverty-stricken neighborhoods. My thought was, "How did they decide not to fix the levees?" It's a hell of a coincidence that the major levee breaches were in heavily Black and poor neighborhoods.<p>
In rich, white neighborhoods, the levees were twice as high. Race calls the shots. If you saw the news, you saw the faces. Why did it take so long for the government to get in there? Had those faces been a different color, I wonder if the results would have been the same.<p>
This is another example of how race and class play an important role in the U.S., down to whether something will be repaired properly. Poor people don't have vehicles; the elderly didn't want to leave. The media have been making it out like people just stayed in the city to loot or were too lazy to leave. But the fact that so many people went to the Superdome shows that they wanted to get out, but just didn't have the means.<p>

<b>WHAT DO you think the future will hold?</b> <p>

THE ONE thing that's depressing is that I don't see anything positive coming. Poor people are being uprooted and brought somewhere else. <p>
The government needs to bear responsibility for a long period of time--to make a lifelong commitment. But I don't know if the government is willing to do that. The government's priority right now is to get the media off its ass. They've been dragged through the mud. So many people are wondering what took them so long. Many people still don't even have the basic necessities.<p>

<b>CAN YOU talk about the legacy of racism in New Orleans, especially what your family faced?</b> <p>

I ALWAYS thought, with Ryan's case, that the prosecutors couldn't get away with what they were trying to do--that they needed to follow legal procedure. But we learned that if a crime was committed against a white person, your ass is grass. In the courtroom, all they said was, "He got what he deserved," even though he insisted he was innocent. <p>
Ryan had an almost all-white jury, the police intimidated him, the prosecutor wore a racist necktie decorated with lynching nooses. Racism definitely exists in American courtrooms. Their goal in that trial was to take any Black man they could find and make him pay the price--they were willing to kill two young people for it. <p>
The racism I saw in New Orleans speaks loudly about how people are being treated now, after the hurricane. At what point will the authorities realize that people won't take this much longer--at what point is enough enough? With my brother, we saw that if you're Black, you're a blemish on America. And that's what we're dealing with today. <p>
There's definitely a lot of similarities between what we experienced in the courts and what we're seeing today. Meeting the needs of Black people doesn't take precedence over anything else--the war in Iraq, in particular. <p>
The media's making a big deal out of looting, but why? We're talking about people losing lives. And that's their focus? That's all about race.<p>

<b>WHAT DO you think the rebuilding of New Orleans should look like?</b> <p>

EVERYONE SHOULD at least have a house. Resources should be evenly divided in the relief effort. The levee system needs to be restructured. <p>
There's been so much destruction in New Orleans, people feel hopeless. The urgency now is huge. People from all walks of life have been affected, but people aren't being affected equally. We need to rebuild the city on a just basis, with better jobs and subways so working people can actually get around. They should have a real plan so that if the levee is breached again, they have a real chance.<p>

