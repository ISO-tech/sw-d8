<font face="Times New Roman, Times, serif" size="5"><b>It's impossible to avoid taking sides</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 9, 2005 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><i><b>Over There</b></i>, airs Wednesday nights on FX.</font><p>

<font face="Times New Roman, Times, serif" size="3">THE NEW television series <i>Over There </i>peers into the lives of a group of eight soldiers in Iraq as well as the emotional trials that face their loved ones back home.<p>
Like producer Steven Bochco's other dramas <i>Hill Street Blues, NYPD Blue </i>and <i>LA Law, Over There </i>breaks new ground, creating the first TV series ever to take as its subject matter a war that's still going on.<p>
In interviews, Bochco has explained that he struggled with "how to go about doing a dramatic show that wasn't a political position paper," because he believes that "politics has no place in entertainment." But of course, there's no way to produce a show about the war in Iraq that's free of any political content.<p>
As a consequence, <i>Over There </i>falls back on the most widely circulated assumptions about the war, which is to say the Pentagon briefings and shallow media coverage that dominate the papers and the evening news.<p>
The compellingly human portraits of the U.S. soldiers and their families take center stage, and the show's creators are careful to show the wide array of motivations that drive the different characters.<p>
There's the gung-ho soldier who signed up for benefits so he can play college football yet loses a leg and can only think of returning to his unit; the Black soldier nicknamed "Smoke" who compares the war zone of Iraq to the war zone of the ghetto where he grew up; and the college-educated philosopher-soldier nicknamed "Dim" by fellow soldiers at boot camp because they figured he must be "stupid to end up in the Army anyway."<p>
But on the Iraqi side of the conflict, there are no compelling characters, no human portraits--just fanatical insurgents, and civilians who are either duped by insurgents or the treacherous allies of insurgents.<p>
In one episode, U.S. soldiers are horrified to find that they've killed a father, mother and child in a car that they've stopped at a checkpoint in a hail of gunfire. But as more cars pass the checkpoint, it turns out that insurgents are indeed smuggling someone in a trunk and that the soldiers conclude that the "innocent" civilians who previously passed by the checkpoint--including the family that they killed--aren't so innocent after all because they're providing cover for the operation.<p>
In another episode, an embedded reporter captures Smoke on videotape killing a young boy and his mother after the boy runs into the middle of a gunfight and throws a bottle. The footage sparks protests throughout the Arab world, but the reporter later shows that insurgents sent the boy into the battle on purpose, sacrificing him and his mother in order to "tarnish" the U.S. war effort. <p>
Therefore, the message--whether intentional or not--is that all Arabs are the enemy. The danger of <i>Over There </i>is its seductive power as a slickly produced story with gripping action and high drama--that claims not to take sides. <p>
It's easy to sympathize with these characters as they face moral conflicts and personal tragedy as U.S. soldiers fighting a war on the barren desert plains and in the dusty villages of Iraq. At the same time, the racist portrayal of the show's Arab characters can't help but elicit pro-war sentiment. <p>
"We didn't come for your oil," yells Sgt. Scream, the unit's leader and emotional center, as he leads the charge on a mosque used by insurgents for cover. "We came to kick your ass." With this rallying cry, Sgt. Scream echoes the cowboy mentality that George W. Bush exudes in his press conferences. But Sgt. Scream's creed doesn't mean that <i>Over There </i>is simply a cheerleader for the war in Iraq.<p>
Other characters "balance out" Sgt. Scream. "The tragedy here is that we're savages, we're thrilled to kill each other," says Dim in a video message to his wife. "We're monsters, and war is what unmasks us. But there's a kind of honor in it too, a kind of grace. I guess if I'm a monster, it's my privilege to be one.'"<p>
But this kind of balance--between the horror of war and the honor of war--misses the point. <i>Over There </i>ignores the central <i>imbalance</i> of the conflict--that an armed-to-the-teeth foreign occupier bent on controlling Iraq's oil and the Middle East as a whole is facing a poorly armed popular resistance trying to free itself from this domination.<p>
And with its slick aesthetic, demonization of "the enemy" and pulse-pounding action, <i>Over There </i>all too easily gets the viewer rooting for the wrong side while it lets the real enemy--the politicians and military brass who sent the soldiers into harm's way in the first place--off the hook.<p>

