<font face="Times New Roman, Times, serif" size="5"><b>Eyewitness tells truth about Guant&aacute;namo</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Elizabeth Lalasz</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 9, 2005 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Erik Saar and Viveca Novak, <b><i>Inside the Wire: A Military Intelligence Soldier's Eyewitness Account of Life at Guant&aacute;namo. </b></i>Penguin Press, 2005, 292 pages, $24.95.</font><p>

<font face="Times New Roman, Times, serif" size="3">SINCE AMNESTY International released its report in early May condemning the U.S. prison camps at Guant&aacute;namo Bay, Cuba, as the "gulags of our times," the politicians in Washington have been scrambling to cover it up.<p>
In late June, after returning from a congressional delegation trip to Guant&aacute;namo, Sen. Ron Wyden (D-Ore.) went so far as to say, "I feel very good" about the detainees' treatment. All this is far from the truth according to Erik Saar and Viveca Novak in their book <i>Inside the Wire: A Military Intelligence Soldier's Eyewitness Account of Life at Guant&aacute;namo.</i><p>
In it, Saar recounts the never-ending cycle of torture. Among the abuses are sleep and food deprivation, mock interrogations, the use of dogs and sexual abuse. Saar writes about all of this first-hand, as he served as an Arabic translator at Guant&aacute;namo from December 2002 to June 2003.<p>
The reader is with Saar "inside the wire" of the prison camps day-to-day for six months with all its horror and desperation. The other compelling thing about <i>Inside the Wire</i> is Saar's transformation from a career soldier who literally volunteers to go to Guant&aacute;namo because he wants to sit face-to-face with the "worst of the worst"--those supposedly responsible for the events of September 11--to someone who deeply questions the U.S. military mission there.<p>
And the stakes were high for Saar, who after his experience at Guant&aacute;namo, felt compelled to resign from the Army and write this book to let the world know that innocent Arabs and Muslims are being detained unnecessarily and tortured for no reason.<p>
As Saar told Amy Goodman in an interview on <i>Democracy Now!</i> in May, "Well, I went there with one expectation. What I found shortly after I arrived, and then I actually went through a process of realizing that my expectations really clashed with the reality of Guant&aacute;namo Bay...There were a number of things that troubled me, that ended up leading me to the conclusion that Guant&aacute;namo Bay, to me, represents a mistake and a failed strategy in this war."  <p>
<i>Inside the Wire</i> is a horrifying and engaging first-hand account and a must read for those looking for the truth about Guant&aacute;namo and about who we need to hold accountable for the horrors going on there right now.<p>
As Saar writes in his conclusion, "What hasn't come out in any of these investigations is an assessment of the leadership's responsibility for the events that transpired at Gitmo. One of the core lessons you learn in the Army, starting at basic training, is that excuses are not tolerated...Why hasn't any leader been held accountable for the failure of Guant&aacute;namo Bay? Why are they allowed to make all the excuses?"<p>

