<font face="Times New Roman, Times, serif" size="4">Walkout tests IAM strength at aerospace giant</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Machinists strike Boeing</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 9, 2005 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">THE STRIKE by 21,000 members of the International Association of Machinists (IAM) at Boeing Co. will determine whether the union will preserve its clout at the world's biggest aerospace company. The strike is also test of whether U.S. manufacturing unions can resist Corporate America's drive to push higher health care costs onto workers today and scaling back retiree benefits in the future.<p>
Workers walked out September 2 after voting 86 percent to reject a contract that would have forced workers to pay health care premiums and eliminate retiree health care for new hires. Management's proposed pension increase was 10 percent--to $66 per year of service--what IAM officials said was the lowest such increase in any contract. <p>
The company tried to split the workforce on the contract by dangling $3,000 cash contract signing bonuses to attract union members who aren't facing retirement soon. Boeing also proposes "wellness credit" that would lower health care premiums for younger, healthier workers. The company also reckoned that the 4,000 laid-off workers rehired since May 2004 wouldn't be eager to forego paychecks to walk a picket line.<p>
Management was calculating that this would deny the union the two-thirds majority needed to both reject a contract offer and go on strike. That was the case in the 2002 deal, when a late-starting and inept contract campaign by IAM officials resulted in a contract being implemented, even though it was voted down by a majority of workers.<p>
This time, the contract was voted down. As IAM District 751 President Mark Blondin put it, Boeing gave the union "an insulting, take-away, job-stealing offer."<p>
Now the IAM is trying to reverse several job-killing and anti-union provisions of the earlier deal. The union's demands include the eliminating contract language that allowed greater outsourcing of IAM work, including the delivery of products to the production line by nonunion vendors. Management wants to keep that language and go further in undermining union rights by having IAM members operate multiple machines in exchange for an extra $1 per hour.<p>
The battle at Boeing--the first strike there in a decade--comes months after management outsourced the Wichita, Kan., production line to the Canadian company Onex. The 5,300 IAM members at that operation-- renamed Spirit AeroSystems--took a 10 percent pay cut in what IAM President Thomas Buffenbarger defends as a "new contract."<p>
This acceptance of concessions at Spirit as "realistic"--which the IAM has done repeatedly in the airlines--has now weakened the struggle at Boeing itself. That's because the IAM contract allows Spirit, as a company independent of Boeing, to maintain production on Boeing's 737 airliner fuselage. The pay cut at Spirit has also given Boeing the confidence to offer no increases at all in pay and benefits to the 900 remaining IAM members in Wichita than their counterparts in Puget Sound.<p>
What's more, the IAM's Buffenbarger has undermined labor solidarity in general by ordering union members to scab on the strike by airline mechanics at Northwest Airlines, who abandoned the IAM in favor of the independent AMFA union several years ago. And, as usual, the engineers' union SPEEA is crossing the IAM picket lines, as required by their union contract--just as IAM members crossed the engineers' lines in previous strikes.<p>
Despite these problems, the Boeing strikers have the power to win--but they need solidarity and support from both wings of the labor movement and working people everywhere.<p>

