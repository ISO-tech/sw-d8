<font face="Times New Roman, Times, serif" size="5"><b>LA activists unite to fight police brutality</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Gillian Russom</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 9, 2005 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES--More than 500 people packed the Bethel A.M.E. church in South Central L.A. August 30 for a rally against police brutality, following the beating of Minister Tony Muhammad of the Nation of Islam by the Los Angeles Police Department (LAPD). Reverend Dr. Louis Logan, the church's pastor, began the rally with a message for the LAPD: "You touch one of our leaders, you touch all of us."<p>
Muhammad and Logan are leading members of Community Call to Action and Accountability (CCAA), a coalition that opposes both police brutality and gang violence. <p>
Tommy Walker, a CCAA organizer who witnessed the incident, spoke out against the mainstream media's attempt to paint Muhammad as the aggressor. "The things you have seen in the news are a lie. To say that this man joined in a 'mob' and attacked two police officers--that's the biggest lie that's ever been told." <p>
The rally was a show of unity between the Black and Latino communities that have both been hard hit by police brutality. The family of Jos&eacute; Raul and Susie Pe&ntilde;a, the Salvadoran-American man and his 19-month-old daughter recently gunned down by the LAPD, were invited on stage repeatedly. The family's lawyer invited the audience to join them in a rally in front of LAPD headquarters September 27 at 11 a.m.<p>
Carlos Lopez, one of the members of the Pe&ntilde;a family, said, "We want justice, especially for the children killed by the LAPD, Devin Brown and Suzy Pe&ntilde;a." Danny Bakewell, founder of the Brotherhood Crusade and publisher of the L.A. <i>Sentinel</i> newspaper, gave a call to action: "We're calling for a 10,000 man and woman march on Parker Center [LAPD headquarters] in two weeks [on September 17] to say we want what everybody else wants--freedom and equality. This is not a Black problem. This is everyone's problem." <p>
The march is also seen as a building block toward the Millions More Movement, a mass protest against racism in Washington, D.C., October 14-16. <p>
Minister Tony Muhammad closed out the rally with his first speech since the beating. "They waited until they had the handcuffs on me and then kicked me in the face," he described. "Black, Brown, red, yellow &#8211; even poor white people don't get justice in this society," he concluded. "I thank God for using me to bring out a systemic problem that the LAPD won't even admit."<p>
A September 17 rally is exactly the kind of action needed to put the LAPD on notice that we won't tolerate anymore of their racist brutality and murder. <p>

