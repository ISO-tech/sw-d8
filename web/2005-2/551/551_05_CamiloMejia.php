<font face="Times New Roman, Times, serif" size="4">Military resister Camilo Mej&iacute;a:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"I pledge my allegiance to the poor and oppressed"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">July 22, 2005 | Page 5</font><p>

<font face="Times New Roman, Times, serif" size="3">CAMILO MEJIA was the first U.S. soldier who served in Iraq to go public with his refusal to continue fighting George Bush's war for oil and empire. <p>
Camilo refused to be redeployed to Iraq before the revelations about torture at the Abu Ghraib prison came to light, but these abuses didn't take him by surprise. One of his first assignments when he arrived in Iraq in 2003 was to detain--and abuse--Iraqi prisoners by depriving them of sleep and using mock executions to terrify them.<p>
A military court forced Camilo to serve seven months' confinement for his decision to abide by his conscience. Since his release, Camilo has thrown himself into building the antiwar movement and counter-recruitment efforts--and speaking about how his time in Iraq has changed his thinking about the world.<p>
On July 3, Camilo spoke at an evening rally against war and empire at the Socialism 2005 conference in Chicago. Here, <i>Socialist Worker </i>prints an extended version of his speech.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">THOSE OF us in the GI antiwar movement, whether we know it or not, face a powerful enemy. When I say antiwar movement, it is assumed that I mean the war in Iraq, but the war in Iraq should be seen as part of something far bigger and far more devastating. <p>
The powerful enemy is the corporations that finance congressional and presidential campaigns, the corporations in control of our privatized government. This is the same enemy that charges the American people a billion dollars per week to send their children to fight a criminal war against the children of Iraq. <p>
Our struggle is the struggle against those who say "support our troops" while turning their backs on returning veterans. It is rather comfortable to say support the troops while keeping their reality in Iraq a mystery. <p>
Support the troops by waving flags and slapping yellow ribbons on the bumpers of SUVs. Support the troops while they are killing their brothers and sisters in Iraq--meanwhile, hiding the flag-draped coffins some of them are coming home in, and keeping the horror of their wounds out of the public's view.<p>
We struggle against those who create terrorism through the spread of hunger and poverty, so they can spread war and reap the profits. We struggle against those who invade and occupy a land for its resources, and then call its people terrorists for refusing to be conquered. <p>
This "terrorism" in Iraq is in reality a fight for freedom and self-determination. It is by twisting the concept of this legitimate struggle into the concept of terrorism, with help from the corporate media, that our puppet government further creates resentment and racism against the oppressed to further its conquest. It is by means of this imperial conquest that a small terrorist network is turned into a global terrorist mentality. <p>
No longer able to rely on the rhetoric of the Cold War, the corporate warmongers need this global terrorism to justify the spread of its empire. So the war we oppose is the war waged by corporations on the billions of people around the world who live in utter misery. <p>
We fight an enemy that can only be made powerful through the systematic exploitation of natural resources and through the constant and systematic poisoning of the environment across the world. This enemy does not need war to spread death and destruction.<p>
We fight a system that feeds on poverty and lack of options to fill the ranks of its imperialist military. <p>
The United States of America is the only superpower on earth--a nation rich and powerful beyond anything ever seen or heard of in history. There is no reason why everyone in this nation should not live a comfortable and stable life. Yet more than 40 million people live without health insurance. The public school system is overcrowded and failing, but to get help from the government, public schools must open their doors to military recruiters. <p>
The so-called American Dream, to many poor people, is tied to the obligation to fight in a war for corporate domination.<p>
They call it an all-volunteer army. But to them, I say: Show me a society where everyone has access to health care. Show me a society where everyone has access to an education. Show me a society where everyone has access to decent wages, where everyone lives a dignified life, and then I will show you an all-volunteer army.<p>
Poverty and oppression around the world provide the building blocks for an empire. Poverty and oppression at home provide the building blocks to build an imperial army. <p>
In saying no to that imperial army--in refusing to fight an imperial war against our brothers and sisters of Iraq--I pledge my allegiance to the poor and oppressed of the world. In saying no to an imperial army and in refusing to fight an imperial war against our brothers and sisters of Iraq, I pledge my allegiance to the working class of the world.<p>
Their struggle--which is your struggle--is my struggle as well.<p>

