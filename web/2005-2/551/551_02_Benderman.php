<font face="Times New Roman, Times, serif" size="4">GI resister Kevin Benderman faces new court-martial</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Put on trial for opposing their war</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 22, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE U.S. Army will again try to court-martial Sgt. Kevin Benderman for refusing to participate in the illegal occupation of Iraq.<p>
After serving one tour of duty in Iraq, Benderman--a 10-year veteran of the Third Infantry Division--refused to deploy for a second tour and filed for conscientious objector (CO) status in late 2004.<p>
During his first tour, Benderman was sickened by the illegal and immoral nature of the war--and the orders he received to shoot Iraqi children and ignore Iraqi civilians pleading for medical assistance. But the Army denied Benderman's CO application and instead charged him with desertion and missing movement.<p>
The first court-martial proceeding was suspended when the military judge ruled that the investigating officer had been biased against Benderman, who faces up to seven years of confinement if convicted.<p>
Benderman was not the only one in his unit who objected to redeployment. Seventeen other soldiers in the same unit went absent without leave, two tried to kill themselves, and one had a relative shoot him in the leg to avoid going back to Iraq. <p>
However, Benderman not only applied for CO status, but he also exercised his First Amendment right to free speech and spoke publicly against the war. For this, the Army is retaliating.<p>
Several of Benderman's supporters--including historian Staughton Lynd and the organizations Veterans for Peace and Voices in the Wilderness--filed a friend-of-the-court brief asking the judge to deny the Army's motion to keep Benderman from presenting evidence regarding the illegal nature of the war and the war crimes he was ordered to commit.<p>
Since the Army alleges that one of Benderman's "crimes" is writing articles and speaking about these illegal orders, the brief points out that the Army itself has opened the door to the question of what Benderman's orders actually were. Once evidence of those orders is introduced, it should be clear that Benderman had not only the right but also the duty to refuse them under the Nuremburg Principles--which do not allow a soldier to escape responsibility for his actions even if following the orders of his superiors.<p>
