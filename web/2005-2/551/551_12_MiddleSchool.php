<font face="Times New Roman, Times, serif" size="5"><b>Targeting kids for war</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">July 22, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">ON JUNE 10, I entered the cafeteria in FHT Middle School in South Burlington, Vt., only to be greeted by a strange sight. A large screen had been set up on the stage, and adults were milling around and passing out flyers. <p>
I glanced down at a flyer on the nearest table and was indignant to find that this invasion of my middle school's cafeteria had been orchestrated by none other than the police and the Vermont National Guard. The point of the presentation was to encourage middle school students to attend a camp that is designed to keep kids "off the streets." <p>
A movie began to play on the screen, and other students and I walked closer, curious. I was repelled by what I saw: kids my age doing jumping jacks in Army-style pants. <p>
It might seem odd that this bothered me, but it wasn't so much what was on the screen as the knowledge that I was forced to endure this if I wanted my school to continue to receive federal funding. That's the issue: The government will take away my school's funding if we refuse to let the National Guard in. <p>
I am 12 years old. I will not be eligible to be in the armed services for another six years. So why do they insist on advertising in my middle school? I can barely tolerate that they are in my sister's high school lobby every day, but a middle school? Trying to recruit me and other kids, barely out of childhood, and telling us we should go to Iraq and die? <p>
I said something to my friend along the lines of, "This is so stupid, I don't want to be recruited!" and a policeman standing nearby immediately latched on. He went crazy trying to convince me and my friend that the presentation wasn't about recruitment, and that I shouldn't judge the camp before going and finding out what it's really about. He was rude and actually stooped so low as to insult me personally. <p>
Is the Guard trying to justify their revolting recruiting tactics by saying that they are coming to schools to "help prevent crime"? If the purpose of the camp is to prevent crime, why are the campers engaging in typical Army activities, such as "security patrol" and "land navigation training"? <p>
Perhaps the answer to all my questions lies in a simple statistic: the Army hasn't met its recruitment goals for the fourth month in a row, and it's becoming desperate. Now the armed forces are trying to recruit and train younger and younger kids. You can always tell a war's gone bad when the government starts targeting children in its search for soldiers. <p>
It has to stop, and soon. It has to stop before one more Iraqi child is killed. Before one more American dies in a pointless and unjust war that is supposed to be over, while we keep sending troops. It has to stop before one more human life winks out because cruel warmongers were put in to power. It just has to stop.<br>
<b>Eliza Leas, </b>Burlington, Vt.<p>

