<font face="Times New Roman, Times, serif" size="4">Mayor won't face trial for gay marriages</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A victory in New Paltz</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 22, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">ALL CHARGES have been dropped against New Paltz, N.Y., Mayor Jason West for marrying same-sex couples.<p>
West, a member of the Green Party, was expected to stand trial in the fall on misdemeanor charges for each of the 24 gay and lesbian marriages he presided over. If he had been convicted, he would have faced fines and up to a year in prison.<p>
Last year, West defied state laws that bar equal-marriage rights by marrying the 24 couples. That made New Paltz, about 75 miles north of New York City, one of several flash points in an explosion of equal-marriage activism around the country in February 2004.<p>
In San Francisco, thousands of gay and lesbian couples lined up for licences when the mayor announced the city would perform same-sex marriages. A few months later, Massachusetts became the first state to marry same-sex couples. Meanwhile, in state after state, supporters of gay rights pressed the demand for equal marriage--with the spirit of a new civil rights movement.<p>
Conservative politicians and the Christian Right counter-attacked, manipulating the supposed "threat" of gay marriage to try to further their cause--and their careers. In New York, Ulster Country District Attorney Donald Williams thought he could make political hay out of putting West on trial.<p>
Today, he's changed his tune, arguing in a letter to the judge that the trial would probably "be exploited by those with a greater interest in publicity than the public good." "While a trial in this case would be filled with rhetoric and hyperbole, it would be lacking in a viable public purpose," Williams wrote.<p>
"He went searching for the limelight, but when it got too hot, he flew away like a moth with singed wings," West's lawyer Joshua Rosenkranz told the <i>New York Times.<p>
</i>Supporters of gay marriage, whose fight has lost momentum over the last year, should celebrate this victory, which shows that the only way to push the bigots back is to stand up to them.<p>
"By recognizing the fact that the more attention is brought to this subject, people will be forced to once again realize that although we are all equal, we are not all treated as such, and we have a long way to go in our society toward that goal," Rebecca Rotzler, the deputy mayor of New Paltz, told <i>Socialist Worker</i>. "We recently held the first-annual Gay Pride celebration here in New Paltz, which brought thousands of supporters...If that is an indication of public opinion in our community, we most certainly have the numbers on our side."<p>

