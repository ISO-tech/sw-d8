<font face="Times New Roman, Times, serif" size="5"><b>Klan targets Latinos in Ohio</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Shane Johnson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 22, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE KU Klux Klan has been distributing racist leaflets in Hamilton, Ohio, after a Mexican man allegedly raped a 9-year-old girl. Several days after the rape occurred, the house that the man was thought to be staying in was burned to the ground. There have also been late night sightings of robed Klansmen attacking Latinos.<p>
Hamilton is a working-class suburb 25 miles north of Cincinnati, and has seen its Latino population boom after the free-market exploitations of NAFTA forced many Mexicans across the border to provide for their families.<p>
Regardless of whatever crime was committed, it's important that racism and the presence of the Klan be directly confronted wherever they rear their heads. The lessons of history show us that passively "ignoring" them will not make the Klan go away. <p>
