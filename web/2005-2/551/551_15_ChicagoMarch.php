<font face="Times New Roman, Times, serif" size="5"><b>40,000 march against Minutemen in Chicago</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Lalasz</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 22, 2005 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--About 40,000 people rallied at McKinley Park on the city's South Side in favor of immigrant rights and in opposition to the Minutemen establishing a chapter in Chicago. <p>
The protest--the largest demonstration in the city in decades--included thousands of immigrants from Mexico, El Salvador, Honduras, Peru, Ecuador, Uruguay and Costa Rica. They chanted at full volume, "&iexcl;Si se puede!, &iexcl;Si se puede!" (Yes, we can!; Yes we can!"), holding signs that read, "Minutemen: Human Rights Violators"; "We want recognition as workers: we are not terrorists"; "We demand the right to get work permits" and "Stop Deportations."<p>
Eliseo Morales, who brought his whole family to the rally said, "We are legal residents. My children were born here and we have no problems with immigration...We are brothers by origin. We come from the same land and share the problems of our race, and so here we are."<p>
Organized through pro-immigrant organizations like Centro Sin Fronteras and local churches, the demonstration took on a bigger character once Spanish-speaking radio disc jockeys began promoting it. Now organizers are floating the idea of organizing a similar demonstration in front of the White House.<p>
The Minutemen--who mounted highly publicized "border patrols" in Arizona and California in recent months--targeted Chicago because it has the second-largest Mexican immigrant population in the U.S. after Los Angeles. That's why the demonstration is so significant. As one young Latino participant told <i>Socialist Worker</i>, "It's the kind of thing you want to see. When you have a good cause, you can bring a lot of people together."<p>
We have to keep organizing. The day before the rally, the <i>Chicago Tribune</i>, ran an interview with the group's main organizers, giving them a platform. These racists are planning a future action targeting Wells Fargo, a bank which accepts the matricula consular, a Mexican ID card primarily used by undocumented immigrants. They're also proposing an "activist boot camp" October 15 to fight "illegal immigration" in Chicago! <p>
We have to build on the momentum of the July 1 protest to let the Minutemen know that they are not welcome here.<p>

