<font face="Times New Roman, Times, serif" size="5"><b>Will the AFL-CIO split in Chicago?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">July 22, 2005 | Page 3</font><p>

WITH RUMORS swirling about backroom deals, boycotts, walkouts and splits, the AFL-CIO convention will take place in Chicago July 25-28.<p>
On one side is the Change to Win coalition--headed by Service Employees International Union President Andrew Stern and Teamsters President James Hoffa--which is demanding a massive restructuring of the federation. Stern wants the AFL-CIO to rebate 50 percent of the dues paid to the federation back to unions that are committed to organizing the unorganized.<p>
The opposing camp, headed by AFL-CIO President John Sweeney, has already moved in Stern's direction, slashing the federation's payroll and agreeing to rebate nearly as much money as Stern and his allies demand. But that wasn't enough to keep Stern from repeatedly raising the prospect of a split in the AFL-CIO to build "something stronger," as he's fond of saying.<p>
However, there's little reason to believe Stern's solution would be any stronger. That's because his faction shares the same fundamental approach as the Sweeney crowd--continued support for the Democratic Party and "partnership" with employers.<p>
Stern opposes the direct election of union presidents and sees union members as soldiers to be ordered around, rather than those with the right to democratically control their own organizations. Thus, his proposals call for big unions to take over smaller ones, and for power to be centralized in the hands of top union officials. And Stern's silence is deafening on the most pressing issue facing labor today--the billions in union concessions to employers in labor strongholds like the airline and auto industries and beyond.<p>
All this explains why the Teamsters' Hoffa--the consummate business unionist--could sign up with Stern, who is usually touted as a social democratic reformer.<p>
However, the dead-end Sweeney leadership offers no alternative. It's noteworthy that Sweeney's allies have had to accept Stern's strategy of mergers simply to survive--including, for example, the recent merger of the former steelworkers and paperworkers unions.<p>
Sweeney and his backers hope that appeals for unity will preserve the AFL-CIO as an institution. But repeatedly calling for "unity" isn't any more of a solution to labor's decline than Stern's mantra of "change." After all, "unity" was the watchword of the do-nothing American Federation of Labor (AFL) in the mid-1930s.<p>
Dissident labor officials launched the Congress of Industrial Organizations (CIO) to catch up with a rank-and-file upsurge already underway--and communists and socialists seized the opportunity to build the CIO on a class-struggle basis. For militants in the 1930s, the question wasn't "labor unity" in the abstract, but building real unity in the rank and file to take the movement forward and win path-breaking contracts in the auto, steel and other industries. <p>
Stern's schemes for restructuring labor from above can't be compared to the CIO. His threats to split the AFL-CIO aren't a solution to labor's crisis, but are a symptom of it.<p>
Unable or unwilling to encourage the kind of militant rank-and-file activism that's needed to revive labor, both factions are arguing over how best to downsize and rationalize the labor bureaucracy to ensure its survival--and are maneuvering over who will control what remains.<p>
When the dust settles after the Chicago convention, union members will face the real task of rebuilding the labor movement--from the bottom up.<p>

