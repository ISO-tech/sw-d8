<font face="Times New Roman, Times, serif" size="4">Strike at Asarco by USW members across the Southwest</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Copper miners draw the line</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jeff Bale</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 22, 2005 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">RAY, Ariz.--Some 750 copper workers went on strike against the Asarco mining company here July 2 as their contract expired. They were later joined by workers from other Asarco mines in Arizona and Texas, who also walked off the job, putting about 1,500 on strike.<p>
The workers, represented by various United Steelworkers (USW) locals, officially are striking against unfair labor practices by Asarco. During contract negotiations, management threatened to void vacation time and cancel health benefits for workers if they went on strike.<p>
The issues behind the strike, however, are even more serious. Although the contract at the Ray mine just expired, workers at the other Asarco mines have been working without a contract for more than a year. <p>
While copper prices are at record highs--bringing in huge profits for the company--management is gunning for the usual cutbacks. The company is demanding wage freezes, increases in health care premiums and pension cuts.<p>
Several strikers said that the company's hard-line stance is retaliation for the union's refusal to accept wage cuts several years ago, when the price of copper had dropped significantly. "This is the most important strike in my lifetime because of what's at stake," said Tino Flores, president of USW Local 915. "The pension is what you work for."<p>
Although most of the strikers are Mexican-American, many--including union officials--cite Asarco's foreign ownership as an issue. The company is owned by Grupo Mexico SA, based in Mexico City. Several strikers have told the local press that management's drive to freeze wages and cut benefits wouldn't have happened under its prior U.S. owners. <p>
This sentiment ignores the brutality of the strikes at New York-based Phelps-Dodge of the 1980s in the last major copper strike in Arizona, which ended in the union being busted. It also forgets the series of strikes at Asarco in the 1970s and 1980s, when it was a wholly U.S. company. <p>
Moreover, the legacy of the 1917 Bisbee Deportation--when strikers were put on boxcars and shipped out of town, also lingers. Most importantly, the idea that the negotiations are tougher with Mexican owners only adds fuel to the vicious anti-immigrant atmosphere plaguing Arizona right now.<p>
Several strikers at Asarco have said that they intend to "lay low" and to ensure that there is no violence at the picket lines. Management has refused to negotiate since the walkout began, and strikers are talking of a lengthy stay on the picket lines. Now is the time to start solidarity efforts, not just in Arizona, but all over North America.<p>
