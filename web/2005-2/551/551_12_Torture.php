<font face="Times New Roman, Times, serif" size="5"><b>Tortured by the prison health system</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">July 22, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">I JUST wanted to thank you for the article exposing the extremely inadequate medical care prisoners in the U.S. receive ("The prison torture scandal at home," June 17). This hits very close to home for me.<p>
My father spent the last 12 years in a maximum security California prison for a drug-related nonviolent crime. That's longer than some people serve for murder. Although he was arrested in Missouri, they moved him to California--not so he could be close to his family, but so they could attempt to try him under the three-strikes law and put him away for life. <p>
Upon his arrest, he was beaten, and his back was damaged. He underwent a botched surgery in a prison hospital that just caused more problems. He has dealt with constant pain and became addicted to painkillers. He also has diabetes, and on the rare occasion that I'd get to talk to him, he'd tell me about how they wouldn't provide the special diet he needed, and how, although he needed insulin, they'd only give him pills to regulate his blood sugar levels.<p>
He was just released a short while ago, and although he went to prison a relatively healthy man, my father is now homeless, jobless, uninsured, mysteriously paralyzed from the waist down and barely coherent! <p>
My family has been trying to get him medical treatment, but because he hasn't had a chance to apply for Medicare, many hospitals refused to accept him. We finally got him into a hospital, and every day, they threaten to kick him out. <p>
The doctors still haven't discovered why he's paralyzed, because they're unwilling to spend the money on tests for an "ex-con" with no health insurance. My family is going to have to cover the medical costs until he gets coverage, which will be inadequate at best.<p>
The price my father and his family have paid--and are still paying--for his crime was too damn high. I've spent half of my life without a father, and the father I have now is not the same person. His life has been destroyed by the criminal injustice system and, ultimately, the system of capitalism that is still punishing him after he has "served his time." <p>
We need radical change to end this sick system that injures people, makes them sick and then denies them health care. That's what I call torture.<br>
<b>Spring Super, </b>New York City<p>

