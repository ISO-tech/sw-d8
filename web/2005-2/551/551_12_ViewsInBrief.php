<p><a name="Crime"></a><font face="Times New Roman, Times, serif" size="4"><b>The corporate crime wave</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WASHINGTON IS sending a message to Corporate America: after a few touchy years, it's cool to be crooked again.<p>
The signs are unmistakable. President Bush has nominated Rep. Christopher Cox, a pro-business fundamentalist, to head the Securities and Exchange Commission. This prospective chief regulator of U.S. stock markets has received over $250,000 in donations from the securities industry. <p>
The Supreme Court recently vacated the conviction of Enron's shred-happy auditor Arthur Andersen in a verdict widely understood to discourage already-difficult prosecution of "white-collar" crimes. <p>
And, in a stunning move, the Justice Department has offered to settle a major federal lawsuit against the tobacco industry for $10 billion--big money, but a tad less than the $130 billion that the government's own expert witness said it would take to launch an anti-smoking campaign to undo generations of tobacco company lies. Even the tobacco lawyers responded with disbelief to this move, rumored to come from the highest levels. <p>
Democrats have responded to all this with their usual slavishness, staying mum or actively championing insane pro-business legislation like the gutting of bankruptcy protections.<p>
Not that corporate scandals have gone away. Insurance giant AIG recently admitted to cooking its books, mutual fund managers are under investigation for using dirty tricks to cheat small investors and drug companies are facing record--and growing--fines for pricing fraud. New disclosures of "improprieties" grace the pages of the financial press every day--although such stories rarely make it into the popular media where working-class people might learn about it.<p>
What kind of justice system throws cancer patients in jail for smoking pot while letting corporate crooks live the high life on stolen money?<br>
<b>Shaun Joseph,</b> Providence, R.I.<p>
