<p><a name="Stereotype"></a><font face="Times New Roman, Times, serif" size="4"><b>Perpetuating a stereotype</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AS A rehabilitation counselor for people living with severe mental illness, I feel compelled to respond to two articles recently published in <i>Socialist Worker</i>.<p>
In his review of the film <i>Cinderella Man</i>, "Seabiscuit in boxing gloves" (June 24), Dave Zirin wrote: "Baer struts around with a psychotic gleam in his eye, as if he would enjoy nothing more than killing Braddock and spitting on his grave. In reality, Baer was devastated by the ring deaths that occurred at his hands, as any non-sociopath would be." In "The New Republic[ans]" (February 18), Zirin used the phrase "psychotic rant" to describe a piece by <i>The New Republic's</i> T.A. Frank in which Frank called for the torture and murder of antiwar activists. <p>
It is clear that Zirin has made the all too common mistake of confusing the term "psychotic" with the term "psychopathic." <p>
For future reference, the latter refers to a person who repeatedly violates the rights of others, and who lacks the capacity to experience guilt or remorse (In a more just world, we would see pictures of Bush, Clinton, and T.A. Frank next to the dictionary definition of "psychopath"!). The former refers to disorders like schizophrenia, in which a person suffers from hallucinations, delusions, disorganized thought and speech, etc. <p>
Contrary to the stereotype perpetuated by the mass media, people with psychotic disorders are generally <i>not</i> violent. In the future, <i>Socialist Worker </i>would do well to avoid contributing--albeit unintentionally--to the already enormous stigma with which people like my clients must cope on a daily basis. <br>
<b>Jenni Rowe,</b> Baltimore<p>
