<font face="Times New Roman, Times, serif" size="4"><b>The Army's false promise</b></font><p>

<font face="Times New Roman, Times, serif" size="3">LET ME begin by saying that the information I read on your Web site was like a breath of fresh air to me. <p>
I was one of the many unfortunate individuals that got taken in with an enlistment bonus for the Army. When I enlisted I went into the delayed entry program on November 2, 2000, for a bonus in the amount of $9,000. "Wow," I said, "that will be a great start when I get out of Advanced Individual Training." <p>
Of course, we all know that we have to pay taxes, but how much? How about paying as much as 69 percent from each disbursement? From my first disbursement of $7000, there was $4,830 taken in taxes, and for each disbursement following in the amount of $500, $345 was taken. <p>
Two years of being both in the military had begun to take its toll on both me and my husband, so I chaptered out. Little did I know I would not be receiving any pay for the month of January, and that they would take the money back for selling almost 40 days of leave time. I am currently paying the remainder of the bonus back today. <p>
It's a long ongoing story. I had problems getting unemployment; DFAS reported the extra income to the IRS in 2002, but didn't notify me of another existing W-2 until this year, so I owe money there as well. <p>
I could go on and on, but it wouldn't change anything. I would just like to say keep getting the truth out, and maybe things will change.<br>
<b>Dante Collins,</b> from the Internet<p>
