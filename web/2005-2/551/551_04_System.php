<font face="Times New Roman, Times, serif" size="4"><b>Right side of town</b></font><p>

<font face="Times New Roman, Times, serif" size="3">MSNBC HAS a funny idea about what constitutes a "town meeting." After George W. Bush's June 28 speech about Iraq, MSNBC's <i>Hardball</i> presented viewers with a "town meeting"--featuring a panel dominated by Iraq war supporters.<p>
The network's two hours of coverage included a panel discussion featuring MSNBC reporter Norah O'Donnell, Islam scholar Reza Aslan, and four conservative Bush supporters: Tony Perkins of the Family Research Council, MSNBC host Tucker Carlson, Bobbie Patray of the Eagle Forum of Tennessee, and Jerry Sutton, pastor of the Two Rivers Baptist Church in Nashville, Tenn., where the event was held. MSNBC also interviewed <i>Newsweek's</i> Jon Meacham, Democratic Sen. Joe Biden (who called for "more boots on the ground"), and Republican Sens. John McCain and John Warner.<p>
In other words, this "town meeting" excluded forceful critics of the Iraq war--a war that polls show most Americans no longer support.<p>
MSNBC's O'Donnell went so far as to say that, while war critics were the majority in the U.S., "at the same time, a majority of Americans also believe that we should stay and finish the job...There are liberal groups like Moveon.org that say we should get out. That's the minority in America. People think that we should stay and finish the job." <p>
Audience participation also tended to support Bush, causing host Matthews to comment: "It's been a great group. As you can see, the people are passionate. And they have strong patriotic beliefs and moral beliefs, and yet it's been very nice here. No fights or anything."<p>
One member of the audience who disagreed with the consensus provided by MSNBC was actually booed by the town meeting audience, causing Matthews to remark: "Don't boo, now, please, ladies and gentlemen. It's been a good night here. Howard Dean is going to come on our program tomorrow, a different point of view. We have diversity run amok." <br>
-- <b>Fairness and Accuracy in Reporting</b>, June 29, 2005<p>

<font face="Times New Roman, Times, serif" size="4"><b>Getting tough on ballet dancers</b></font><p>

<font face="Times New Roman, Times, serif" size="3">TED SCHELENSKI, a retired Navy lieutenant and current vice president for finance and operations at the right-wing Heritage Foundation, recently demonstrated how tough conservatives really are. He assaulted a ballet dancer half his size. <p>
The incident occurred when Schelenski, in his car, blocked the path of a bicyclist and refused to move. A shouting match ensued between Schelenski and the bicyclist, Kristin Hall. Schelenski then got out of his car and came at the 105-pound Hall, shoving her to the ground while she was still straddling her bicycle.<p>
"It was some kind of road-rage nonsense," Hall said. "When he got out of the car, I told him: 'You're crazy! Get back in the car!' I was pretty scraped up and bruised. And he just got back into his car and floored it. He took off."<p>
Bystanders followed Schelenski as he drove down the block to his nearby office. About 10 minutes later, he returned to the scene. "He said he lost his temper," said Hall. "And then he told the officer that all he did was try to shake my bike. He said I was the one who fell over."<p>
Police arrested Schelenski, who is due back later this month to face an assault charge.<br>
-- <b><i>Washington Post</b></i>, July 2, 2005<p>

