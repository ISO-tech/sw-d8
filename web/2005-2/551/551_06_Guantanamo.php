<font face="Times New Roman, Times, serif" size="5"><b>How the U.S. tested its torture techniques</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 22, 2005 | Pages 1 and 6</font><p>

<font face="Times New Roman, Times, serif" size="3">THE U.S. military's tactics of torture and humiliation pictured in the sickening photos from Abu Ghraib prison in Iraq were "road-tested" at its prison camp at Guant&aacute;namo Bay, Cuba, according to a Pentagon investigation.<p>
A report turned over to the Senate Armed Services Committee says that interrogators at Guant&aacute;namo forced Mohamed Qahtani--the alleged "20th hijacker" in the September 11 attacks--to wear women's underwear on his head, threatened him with snarling military dogs and tied a leash to his prison chains. This happened several months before the U.S. invaded Iraq--eventually taking over management of Saddam Hussein's prison at Abu Ghraib.<p>
Defense Secretary Donald Rumsfeld personally approved a list of "harsh" techniques as a way of getting Qahtani to talk, according to military investigators.<p>
The inquiry at Guant&aacute;namo Bay was into allegations of prisoner mistreatment documented by the FBI. But the military's conclusion was that its interrogation techniques were almost all "authorized," and therefore could not be legally considered mistreatment.<p>
When the pictures from Abu Ghraib shocked and outraged the world last year, the Bush administration pinned the blame on a "few bad apples"--lowly enlisted personnel acting on their own. But the Pentagon report proves that the torture tactics at Abu Ghraib were used many months before at Guant&aacute;namo.<p>
The man responsible for the abuse of Qahtani, according to the Pentagon report, is Major Gen. Geoffrey Miller, commander of the Guanatanamo prison camp.<p>
In September 2003, Miller traveled to Iraq to help set up the U.S. command at Abu Ghraib, and he sent his "Tiger Teams" of interrogators and analysts from Guant&aacute;namo to be advisers and trainers at Abu Ghraib. "Within weeks of [Miller's] departure from Abu Ghraib," the <i>Washington Post </i>reported, "military working dogs were being used in interrogations, and naked detainees were humiliated and abused by military police soldiers working the night shift."<p>
Nevertheless, Miller escaped any punishment, even for the abuse at Guant&aacute;namo that the Pentagon investigation said he was responsible for, because his commanding officer refused to take action.<p>
Despite the further evidence of abuse, however, the Bush administration won another legal round in its battle to use unaccountable military tribunals for the detainees at Guant&aacute;namo. On July 15, a federal appeals court gave the Pentagon permission to resume war crimes trials of detainees.<p>
The ruling overturned a judge's decision last year that the tribunals were patently unfair, and that Bush had overstepped his constitutional authority and ignored the Geneva Conventions on handling prisoners of war. Days earlier, another federal judge refused to stop Guant&aacute;namo interrogators from questioning a Canadian detainee--a 15-year-old at the time of his capture in Afghanistan in 2001--who says he was tortured.<p>

