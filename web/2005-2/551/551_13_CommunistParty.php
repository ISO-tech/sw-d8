<font face="Times New Roman, Times, serif" size="5"><b>The CP adapts to the right...again</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 22, 2005 | Page 13</font><p>

<font face="Times New Roman, Times, serif" size="3">THE COMMUNIST Party (CP) at its recent convention in Chicago reaffirmed its support for the Democratic Party.<p>
"The Bush administration is a right-wing authoritarian regime," explained CP national chair Sam Webb in his opening remarks, "and broad sections of the labor movement--center forces, social democrats, Democrats, social reformists, liberals, even some Republicans--are opposing its policies and battling capitalist globalization as well.<p>
"Don't we have to unite with them," he concluded, "in fighting policies of the Bush administration?"<p>
Webb cited as a key political goal "Taking back Congress in 2006," by which he clearly did not mean running CP candidates for Congress but securing a Democratic majority.<p>
Though the party today is a shadow of its size and influence in the 1930s, its political outlook remains virtually unchanged since the CP adopted its "popular front" strategy in 1935. "The main enemy of the people of America today," then-CP leader Earl Browder announced at a May 20, 1936 meeting in Madison Square Garden, "is the Republican-Liberty League-Hearst combination. We must place as the center of our work in the election campaign the need for combating this reactionary bloc and defeating its plans in 1936."<p>
The "people's front" had its origins in Stalin's desire to cement alliances with various capitalist states against the threat of Hitler's Germany.<p>
The parties were instructed to make an about-turn after the rise of Hitler. From denouncing all social democrats and reformists as "social fascists," in some cases worse than fascism itself, the Communist Parties now swung around to make completely uncritical alliances not only with reformist workers' parties but with middle-class and bourgeois parties.<p>
In the U.S., this took the form of the "democratic front," that is, uncritical support for Roosevelt and the Democratic Party. The CP declared that forces "within and around" the Democratic Party were a key component of the People's Front in the U.S.<p>
According to CP leader Gene Dennis, this "democratic front" included not only of workers and middle-class people but also "important sections of the upper middle class and certain liberal sections of the bourgeoisie."<p>
Browder was worried that open CP support would actually cost Roosevelt support, so he was given broad leeway to determine how best to back him. Hence the CP called for a "defeat of reaction," rather than openly campaigning for Roosevelt.<p>
As for this "democratic front," the CP was happy to play the hunting dog. CP leader Clarence Hathaway explained in 1938, though the CP "will not be admitted into this progressive force," the party should "support the progressive movement, not demanding the admittance of our party, not making this a condition for our support of the democratic forces, but showing by our activity in the campaign, by our energetic support for the progressive candidates, that our Party is a constructive force entitled to entrance."<p>
Thus, the CP's own rationale for the "democratic front" openly proclaimed the need for the party to completely subordinate itself to the Democratic Party, to which it had to "prove itself."<p>
From this point on, aside from a brief interlude when the party did another about-face during the Hitler-Stalin pact and briefly returned to a formal policy of opposition to Roosevelt, it consistently attacked any attempt to build any third party outside the Democrats as playing into the hands of "reaction."<p>
The CP denounced left-wing critics of Roosevelt as "agents of fascism." During the war, the CP became a fervent cheerleader of the U.S. war effort, even going so far as agreeing with the unions' no-strike pledge and supporting the internment of 120,000 people of Japanese descent, calling it "a necessary war measure."<p>
The logic of the popular front was to adapt rightward, and the same logic is on display today. The party's magazine, <i>Political Affairs, </i>recently published an article entitled "Gay Marriage: Too Much, Too Early." It would be farcical if it weren't so tragic.<p>

