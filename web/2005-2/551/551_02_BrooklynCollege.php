<font face="Times New Roman, Times, serif" size="5"><b>Witch-hunt comes to Brooklyn College</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Joe Cleffie</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 22, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE ATTACK on academic freedom that has intensified since September 11 has turned to Brooklyn College in New York City. The attack dogs in the New York media are calling the school "little Columbia"--a reference to Columbia University, where professors in the Middle Eastern and Asian Languages and Cultures (MEALAC) Department were wrongly accused of anti-Semitism and supporting terrorists by right-wing and Zionist students. <p>
The attack at Brooklyn College has centered on two teachers. One is Tim Shortell, a professor who was recommended to be appointed chair of the school's sociology department. Then, the media discovered that Shortell had written an article called "Religion and Morality: A Contradiction Explained," which called Christians "moral retards" and said that Christianity preached "self-righteousness" and "hatred," despite the idea that it is a religion "based on love."<p>
While Shortell's article could be called elitist, it certainly gives a fitting description of the bigots on Christian Right who are so influential in the Republican Party. And far worse is regularly said about Islam in mainstream media publications.<p>
Stories quickly appeared in the <i>New York Daily News </i>and <i>New York Sun </i>calling for Shortell's appointment to be withdrawn.<p>
As at other universities where faculty have come under fire, the administration caved. Brooklyn College President Christoph Kimmich called the essay "offensive" and formed a committee to investigate if Shortell would be "biased against religious students."<p>
A number of Jewish, Christian and Muslim students wrote a statement supporting Shortell, saying that he had a right to express his views and that colleges should be a place where freedom of speech was protected. But the emerging battle over his appointment as department chair was cut short when Shortell withdrew.<p>
The other major attack on academic freedom has targeted Priya Parmar, an assistant professor in the Education Department. Right-wing students who promote David Horowitz's misnamed "Academic Bill of Rights"--aided by the <i>New York Sun--</i>accused Parmar of saying that English is the "language of the oppressors."<p>
In fact, this quote comes from one of the reading assignments from her class. But the witch-hunters aren't about to let such facts get in the way of their campaign--which is especially serious since Parmar does not have tenure.<p>
The right wants professors to be scared about what they teach. But as Sam Farber, a Brooklyn College professor of political science, put it, "The best defense against this is a good offense. Just teach in the usual way." Also key to this offense will be students and professors organizing to defend free speech in the classroom when classes resume in the fall. <p>

