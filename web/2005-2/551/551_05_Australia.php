<font face="Times New Roman, Times, serif" size="5"><b>Antiwar activists reach U.S. sailors in Australia</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 22, 2005 | Page 5</font><p>

<font face="Times New Roman, Times, serif" size="3">ANTIWAR ACTIVISTS in Sydney, Australia, turned the harbor area surrounding three U.S. Navy ships into a huge, open-air theater--and found that U.S. sailors warmly received their leaflets and the antiwar documentary they projected.<p>
Using a projector and huge sound system, organizers used the side of a shipping container next to the Navy ships to project <i>Sir, No Sir,</i> a new documentary by David Zeiger about the revolt in the U.S. military during the Vietnam War.<p>
"We estimate that at least 700 personnel saw some of the movie," wrote James Courtney, a peace activist and member of Greenpeace, in a report he sent to the Internet newsletter <i>GI Special</i>. "Around 200 or so watched it for 10 to 15 minutes. The U.S. MPs and around 15 plainclothes U.S. military staff watched the whole movie (it was right in their face so they didn't have much choice). The sound system was very loud, so we expect that possibly thousands of personnel heard what was happening."<p>
U.S. Navy officers asked local police to stop the activists, but the police replied that they had a democratic right to protest. <p>
Activists also found other ways to reach out to the sailors. "At the main gates of the naval base through which all service personnel coming or going passed...we laid out candles in the shape of the peace symbol," wrote Courtney. "The response was moving and inspiring. We had naval personnel helping to light candles and taking photos. Many words of thanks from ships' crew--some with tears welling in their eyes. <p>
"We had nothing but positive feelings from the ships' crew that spoke with us. "We managed to hand out around 200 copies of <i>Traveling Soldier, </i>but noticed that that this was mostly from crew going ashore, and that many were cautious about taking it..."<p>
"This was a very moving and inspirational experience for the small group of people that made it happen. We felt moved by the plight of the young men and women that we met. There was one line that we heard from many: 'Sometimes, I feel that we are fighting for the wrong reason.'"<p>
The creativity that these antiwar activists used in reaching active-duty military personnel--and the friendly reception they received from the sailors--is inspiring. They provided a shining example of what's possible at a time when Bush's war for oil is growing increasingly unpopular--in the U.S., around the world and even within the ranks of the U.S. military.<p>

