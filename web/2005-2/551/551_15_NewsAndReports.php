<p><a name="Abortion"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend abortion rights</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Sabrina Fletcher and Randy Childs</b></font><p>

<font face="Times New Roman, Times, serif" size="3">PRO-CHOICE demonstrators took to the streets in California in mid-July to confront right wingers and stand up for abortion rights.<p>
-- In <b>San Francisco</b>, about 40 abortion rights supporters stood up against a group of about 15 anti-choice protesters July 16 in front of the Planned Parenthood Golden Gate clinic, staging one of the first clinic defenses that this city had seen in many years.<p>
This counter-protest was held in response to the arrival of a traveling group of college-aged youths from the American Life League, a right-wing anti-choice Catholic group. These anti-abortion bigots who are on what they call a "Crusade for Life," were met by an abortion-rights coalition made up of groups from around the Bay Area including the International Socialist Organization, the Freedom Socialist Party, Radical Women, Code Pink and other advocates for abortion rights. <p>
The pro-choice group surrounded the bigots, dancing with their signs, and continued to rally in support of the clinic, even through the "die-in" staged by the anti-abortionists, who pretended to drop dead to the ground, curling up in the fetal petition for over an hour. <p>
Responses from passing San Franciscans were overwhelmingly positive, with a general feeling of relief that their opinions were finally the ones being shouted into the streets. The counter-protesters' refusal to stand quietly by and let the bigots be the only ones heard was greeted by elated honks from an abundant amount of cars, three passing Muni public buses and even a Lutheran church van.<p>
-- In <b>Los Angeles</b>, a day earlier, about 50 women and men rallied at a busy intersection in western Los Angeles on July 15 to stand up for a woman's right to choose. Protesters demonstrated a level of determination and energy that is sorely needed in the struggle for abortion rights.<p>
For more than an hour, activists circled the intersection, crossing the street with every light change, waving signs that said, "Honk for Choice!" The enthusiastic response from passing rush-hour drivers created a practically uninterrupted symphony of car horns. <p>
