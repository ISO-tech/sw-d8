<font face="Times New Roman, Times, serif" size="4"><b>Get military recruiters out of our schools</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Alessandro Tinonga</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO&#8212;Volunteers for the "College Not Combat" campaign submitted petitions with more than 15,400 signatures on July 11 to the city of San Francisco to put a counter-recruitment initiative on the November ballot.<p>
The measure encourages city officials and university administrators to exclude military recruiters from schools and campuses and to create scholarships and training to reduce the military's appeal to youth. "This initiative strikes at the core of the War Machine," said Aimee Allison, a veteran and Green Party candidate.  "When they [the recruiters] say they'll pay for college, it's a lie. They promised me $10,000, and I never saw one cent."<p>
For six weeks, the all-volunteer campaign has been aggressively collecting signatures, easily surpassing the minimum of 10,000 signatures required to get the measure on the ballot. "It was relatively easy to gather signatures," said Ragina Johnson, a volunteer organizer. "Many people are fed up with the war and the lies that are told to our students and were willing to sign up."<p>
More than 50 organizations and political leaders have publicly endorsed the ballot initiative, including the American Federation of Teachers Local 2121, Service Employees International Union Local 790, United Educators of San Francisco and the San Francisco Labor Council.<p>
The final push to gather signatures took place on June 9, where 100 volunteers showed up to gather more than 4,000 signatures.  <p>
-- In <b>New York City, </b>about 50 people came from across the city for a vibrant and well-received demonstration in Brooklyn's Fulton Mall. Demonstrators handed out flyers and targeted the large military recruitment station strategically placed in this mainly Black, working-class neighborhood.<p>
The event was the first of many mobilizations that will be held this summer to target recruiters throughout the city.<p>
The New York City Counter-Recruitment Committee, which grew out of a well-attended Educators to Stop the War conference this past spring, called for the event, and many organizations attended, including the Campus Antiwar Network, the United Federation of Teachers to Stop the War, Brooklyn Parents for Peace, the International Socialist Organization, the local chapter of Military Families Speak Out and others.<p>
The level of community support for the counter-recruiters was energizing and shows that there's a large audience that can be mobilized for the national antiwar mobilization in Washington, D.C. on September 24.<p>
<i>Aaron Amaral contributed to this report</i>.<p>
