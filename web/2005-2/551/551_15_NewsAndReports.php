<p><a name="SFSU"></a><font face="Times New Roman, Times, serif" size="4"><b>The right to protest</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Katrina Yeaw</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO&#8212;Attorneys from the National Lawyers Guild filed suit against San Francisco State University (SFSU) July 15, 2005, on behalf of the International Socialist Organization (ISO) and Students Against War (SAW), a chapter of the Campus Antiwar Network.<p>
The suit arises from the SFSU administration's response to a March 9, 2005 counter-recruitment protest on campus. That day, some 200 students rallied in Malcolm X Plaza and then marched into the Cesar Chavez Student Center to confront Army and Air Force recruiters, who eventually abandoned their tables.<p>
SFSU retaliated by attacking both individual student activists and organizations involved. The sanctions include denying funding for both the ISO and SAW in the 2005-2006 academic year, and forcing the leadership of the ISO to go through training on the university's demonstration guidelines before it can have any more events.<p>
