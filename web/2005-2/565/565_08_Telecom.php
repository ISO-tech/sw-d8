<font face="Times New Roman, Times, serif" size="5"><b>Ripping off N.Y. consumers</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 11, 2005 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">A NEW York state government agency is planning on deregulating the telecommunications industry. Rates for basic local service will increase by up to 65 percent for many customers if the New York Public Service Commission (PSC) pushes through its plans. <p>
Shamefully, phone companies already overcharge, especially for long-distance and collect calls from prisons. Verizon can cut off phone service if a customer is past due only $8, and then charge $36 to turn the phone back on.<p>
What's worse, New Yorkers may be paying more money for lower-quality service. The PSC already lifted regulations regarding service quality for phone giant Verizon, even though Verizon was penalized more than $200 million for poor service over the last 10 years--including $15 million last year. Now, the PSC wants to get rid of most of the remaining standards.<p>
Call the PSC at 800-342-3377  or e-mail <a href="mailto:secretary@dps.state.ny.us">secretary@dps.state.ny.us</a> to tell them not to make life tougher for poor and working-class New Yorkers.<br>
<b>Dominic Renda, </b>New York City<p>


