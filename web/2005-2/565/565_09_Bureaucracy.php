<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Capitalism and bureaucracy</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 11, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">A COMMON critique of socialism is that capitalism is far more efficient and less bureaucratic.<p>
"No private enterprise," wrote sociologist Ludwig von Mises in 1944, "will ever fall prey to bureaucratic methods of management if it is operated with the sole aim of making profit." According to Mises, only government interference in this process created bureaucracy. Mises was an advocate of free-market capitalism at a time when Keynesianism ruled the roost.<p>
Today, Mises' ideology has won out, yet his ideas fail utterly to describe reality. In spite of the move away from public ownership and toward private ownership, governments around the world have continued to take taxpayers' money and essentially use it to subsidize private industry and to enhance the instruments of repression and war.<p>
Amid all the talk about the declining role of the state, state expenditure as a percentage of GDP has dramatically increased over the last quarter-century. The 1997 World Bank World Development Report showed that in the OECD countries, total government expenditure as a percentage of GDP grew enormously since 1960, when it was just under 20 percent of GPD, to just under 50 percent by 1995.<p>
What about private industry? Has allowing the free market to rip meant less corporate bureaucracy? Not really. Through the first half of the 1990s, the heyday of neoliberal capitalism, the corporate bureaucracy in the U.S. grew considerably.<p>
According to author David Gordon, the number of managers and CEOs in the U.S. grew from 12.5 to 14 percent of the workforce.<p>
Corporate bureaucracy is not a product of "state interference" in the free market, but of the concentration and centralization of capital, the growth of the world market, and with it, the development of monopoly control of entire industries and markets by handfuls of giant transnational corporations.<p>
These companies compete on an international market, but must also engage in large-scale internal planning that involves armies of lawyers, managers, lobbyists, consultants and so on.<p>
If anything, free-market neoliberalism, with its open nod toward the goodness of rampant unregulated greed, breeds corporate bureaucratism. The profit motive creates a culture of corruption and cronyism in which various players are constantly seeking ways to get their hands on a share of the wealth drawn from the sweat of the workers employed by the particular firm and from state funds.<p>
Wherever profits are overflowing, there are hangers-on, crony friends and no-show "consultants" feeding at the trough. The history of U.S. business if full of stories of big businesses, and even entire industries, that, drunk with the success of a certain organizational and business model, develop bloated, complacent bureaucracies that are then jolted by new competition from unexpected quarters.<p>
A look at the health care system in the United States completely destroys the argument that private is less bureaucratic than public. The private, for-profit health care industry in the U.S. generates a massive bureaucracy whose sole purpose is to perform the paperwork involved in ensuring that revenues and profits flow to the "right" people.<p>
A 2004 study by Harvard Medical School researchers and Public Citizen found that the health care bureaucracy cost the U.S. $399.4 billion, and that a national health insurance system could save at least $286 billion annually on paperwork. That would be enough money to provide all of the 43 million uninsured in the U.S. plus provide full prescription drug coverage for everyone in the U.S.<p>
The study found that bureaucracy accounts for 31 percent of U.S. health care spending, whereas in Canada, whose national health care system still hangs on, bureaucracy accounts for only 16.7 percent of health care spending and manages to provide more health services per dollar spent.<p>
A nationally funded and planned health care system in the U.S. would deliver more service for less cost. But then again, health care in the U.S. isn't about helping people, but about a tiny number of capitalists making a large amount of money.<p>


