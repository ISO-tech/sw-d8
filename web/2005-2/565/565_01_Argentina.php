<font face="Times New Roman, Times, serif" size="5"><b>Bush faces huge protests in Argentina</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 11, 2005 | Page 1</font><p>

<font face="Times New Roman, Times, serif" size="3">HUGE PROTESTS greeted George W. Bush's visit to Argentina as the Summit of the Americas ended without negotiating the free trade deal the U.S. government had hoped for.<p>
As 34 heads of state met in Mar del Plata, a seaside resort city south of the Argentine capital of Buenos Aires, to discuss Washington's plan for a trade agreement to expand NAFTA throughout the hemisphere, tens of thousands of protesters took to the streets.<p>
People came from across Argentina and beyond to voice their opposition to the U.S. war on Iraq, and Bush's "neoliberal" economic agenda of free trade, government cutbacks and privatization. They were met by huge numbers of police in full riot gear, who fired volleys of tear gas into the crowd. Later, small groups of demonstrators rioted, trashing businesses and setting at least one bank on fire.<p>
Argentine soccer star Diego Maradona helped lead the protests. He arrived with other demonstrators on a private train sponsored by Venezuelan President Hugo Ch&aacute;vez, an arch-enemy of Bush. Ch&aacute;vez and Maradona were among the speakers at a massive rally in the Mar del Plata soccer stadium.<p>
"The city was plastered with 'Stop Bush' posters, everywhere you looked," said Ahmed Shawki, editor of the <i>International Socialist Review, </i>who attended a counter-summit organized by left-wing and global justice groups. "A couple days before the Summit of the Americas had even opened, any American-owned company, from Blockbuster Video to Dunkin' Donuts to Citibank, was boarding up their windows in anticipation of protests against the summit and Bush's presence. <p>
"When Bush finally arrived, everything was shut down for security--including where the summit was being held. Residents from the area were given identification cards that they had to show to enter and leave their homes."<p>
The backdrop for the meeting was a series of revolts in countries across Latin America since Bush attended his first Summit of the Americas in Quebec City, Canada, in 2001. Mass protests have brought down presidents in Ecuador, Bolivia, Peru and Argentina itself.<p>
The Argentine economy--which once boasted living standards comparable to many European countries--is still suffering the effects of an economic depression brought on by austerity policies dictated by the International Monetary Fund (IMF).<p>
The mass uprising of December 2001--known as the Argentinazo--drove out the hated government that did the IMF's bidding. Argentina is now ruled by the center-left government of N&eacute;stor Kirchner, which has attempted to demobilize popular organizations that arose out of the Argentinazo, through granting some concessions--while maintaining the substance of free-market policies of the past.<p>
"Much of the social movement's leadership is tied up with Kirchner now, and didn't want to embarrass the president by mobilizing," Shawki said. "This is part of a conscious strategy of cooptation--to pick off certain organizations and demobilize some forces, which has left other parts of the movement open to attack. For example, there's a very sharp attack on some of the workplaces that were opened up under workers' management after the economic collapse."<p>
By contrast, Ch&aacute;vez was a lightning rod for discontent. His radical program of standing up to U.S. imperial power and challenging Venezuela's corrupt ruling class while enacting important social reforms has found a hearing throughout Latin America. As U.S. officials tried to hold the summit together, Ch&aacute;vez told the enormous soccer-stadium crowd that the FTAA would "be buried" after this summit.<p>
In fact, the Bush administration's greatest fear seemed to be that the two presidents would meet face to face--something administration handlers worked overtime to avoid. After weeks of setbacks at home, Bush left Argentina with the vast international opposition to his right-wing agenda demonstrated for the world to see.<p>



