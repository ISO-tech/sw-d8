<font face="Times New Roman, Times, serif" size="5"><b>NYU grads vote to strike</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Mitch Day, GSOC</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 11, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Graduate assistants at New York University (NYU) voted October 31 in favor of striking against NYU's refusal to negotiate with our union. More than half of the roughly 1,000-strong membership of the Graduate Student Organizing Committee (GSOC)/United Auto Workers Local 2110 voted, and 85 percent voted in favor of striking. <p>
This strong showing is a result of both hard work during the past year by rank-and-file union members as well as the administration's particularly obnoxious refusals to negotiate with us.<p>
The strike is set to begin November 9, and picket lines will focus on the Silver Center, a building housing hundreds of classes each day, with a roving picket hitting various campus buildings. <p>
After the strike vote was announced, the administration wasted no time in sending out a mass e-mail to the university community with the subject line, "Vote by the United Auto Workers to disrupt classes." <p>
NYU insists on referring to GSOC as "the auto workers union" in an attempt to paint our union as an outsider to academia with decisions being made by industrial workers. But many professional workers are united under UAW Local 2110, including Columbia graduates, <i>Village Voice </i>reporters and curatorial assistants at the Museum of Modern Art. <p>
NYU has refused to meet with GSOC's negotiating committee, which is made up of graduate assistants.<p>
Already, more than 400 faculty have submitted requests to the union to help them move their classes off-campus. Coverage of the upcoming strike has dominated the school newspaper, with staff editorials solidly pro-GSOC--one ending with, "We won't cross any picket lines." Members of Grad-Undergrad Solidarity have been tabling on campus and plan to distribute pro-GSOC T-shirts to undergrads during the strike.<p>
GSOC has been a thorn in NYU's side ever since it demanded and won pay increases and full health coverage four years ago. As the only private university with a unionized graduate workforce, it will take more than symbolic protest to stop NYU's union-busting. The effectiveness of this strike will depend on the extent to which university function is disrupted. <p>
Unfortunately, union leaders are calling for a symbolic picket line. While teaching assistants are called upon to stop teaching and join the picket, undergraduates, faculty and staff aren't being asked to honor picket lines. As Susan Valentine, a graduate who spoke to the school newspaper on behalf of GSOC, said, "Our picket lines are not impermeable barriers, but can be respected in solidarity." <p>
It should be clear that after our hundreds-strong rallies, petitioning and support from city councilors, NYU will not give in to symbolic pressure--but also that it's possible to build more than symbolic support. A teaching strike with undergrads, faculty and staff honoring our pickets will bring NYU to the bargaining table and give our strike the best chance at success.<p>
