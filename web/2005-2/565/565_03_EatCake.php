<font face="Times New Roman, Times, serif" size="4">Budget cuts will cause more suffering for victims of Katrina</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The "let them eat cake" Congress</b></font> <p>
<font face="Arial, Helvetica, sans-serif" size="2">November 11, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">DAYS AFTER Hurricane Katrina hit the Gulf Coast, the only Federal Emergency Management Agency (FEMA) official on the ground in New Orleans reported about the desperate situation. Conditions in the Superdome, where thousands were running out of food and water, were "past critical," Marty Bahamonde wrote in an urgent e-mail to his boss.<p>
Unfortunately for those trapped in the Superdome, the recipient of Bahamonde's e-mail was then-FEMA Director Michael Brown. Brown's reply: "Thanks for the update. Anything specific I need to do or tweak?"<p>
If this response shows a certain lack of urgency, perhaps it was because Brown was so busy with other matters. Other Brown e-mails, released last week by members of Congress investigating the federal response to Katrina, showed him discussing weighty matters like what shirt to wear for his television appearances and finding a sitter for his dog.<p>
By now, "Brownie," as George Bush called him, is out as FEMA director, and in as the butt of late-night TV jokes--although, incredibly, his contract as a consultant to the agency, at a cool $148,000 a year, was recently extended for another month.<p>
But Brown's "let them eat cake" attitude toward the victims of Katrina is still easy to find in Washington. The House Republican majority just pushed through $54 billion in spending cuts, allegedly to cut the budget deficit and make room for massive aid to Gulf region.<p>
The cuts will compound one disaster with another--this one entirely unnatural and fully of their making.<p>
The budget ax will hit hardest at Medicaid, the main health care program for the poor, with $9 billion in cuts this year and as much as $45 billion over the next 10 years. In human terms, this means up to 6 million people--including children, the elderly and people with disabilities--will lose health benefits under Medicaid.<p>
Another target of the budget hawks is food stamps. The cuts imposed on this program to subsidize food purchases for the poor would push 225,000 working families--and as many as 330,000 children--from the rolls, according to the Center for Budget and Policy Priorities. Forty thousand children could lose subsidized school lunches, and aid to foster families will also be cut by almost $400 million.<p>
It doesn't take a genius to figure out that a substantial number of the people to bear the brunt of these cuts will be the same people who lost their houses and jobs because of the Gulf Coast hurricanes. This makes a mockery of congressional claims that $54 billion in budget cuts is the price that had to be paid to finance reconstruction after Katrina.<p>
Meanwhile, consider that the House and Senate left planned tax cuts of $70 billion--which will go to the richest people in the country--untouched. In fact, the idea of scaling back these gifts to the rich--including the planned phase-out of the estate tax paid only by the richest 2 percent of families--wasn't even up for discussion in Congress.<p>
The House budget plan also made no effort to get rid of billions of dollars in pork-barrel projects--like the so-called "bridge to nowhere" in Alaska, a planned new highway bridge to a tiny, nearly uninhabited island, whose elimination from the budget would go halfway to restoring the cut in the foster care program.<p>
Even the normally dull liberal <i>Washington Post</i> columnist E.J. Dionne had to express his "outrage." "Isn't it remarkable that congressional conservatives who think we can afford $70 billion worth of tax cuts in this budget--meaning the budget actually increases the deficit--can't come up with that $400 million for foster kids?" Dionne asked.<p>
For a brief period, Hurricane Katrina exposed the ruin that federal neglect has caused for millions of poor and vulnerable people in the U.S. But after the immediate crisis passed, the Washington politicians got back to the status quo--of stealing more from the poor to give to the rich.<p>

