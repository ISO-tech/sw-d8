<font face="Times New Roman, Times, serif" size="4">Protest targets police who blocked evacuees</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Return to the Gretna bridge</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 11, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">PEOPLE FROM around the country joined New Orleans activists November 7 for a protest march over a bridge that connects the city to the mostly white suburb of Gretna.<p>
During the Hurricane Katrina disaster, Gretna police closed the bridge to evacuees fleeing the devastation in New Orleans--at least one time firing over the head of a crowd of some 1,000 people who had walked to the bridge after being assured that buses were waiting on the other side to evacuate them.<p>
Organized by the newly formed Hip Hop Caucus and United Progressives for Democracy, the protest began with a rally at the New Orleans Convention Center. The demonstrators then crossed the Crescent City Connection Bridge spanning the Mississippi River and marched to Gretna's Oakridge Mall, where the evacuees were told they would find buses.<p>
Among those who called for the protest was Rep. Cynthia McKinney (D-Ga.), who recently introduced legislation to strip federal funding from the Gretna Police Department and the sheriff's department of Jefferson Parish for a year because of the confrontation at the bridge.<p>
McKinney was joined by a mix of political figures and veteran activists, including National Organization for Women President Kim Gandy, Curtis Muhammad of Community Labor United, and Ron Daniels of the Center for Constitutional Rights.<p>
"Instead of meeting people who are sworn to protect and serve us, these citizens met armed vigilantes in one of the most racist encounters ever documented in America," Rev. Lennox Yearwood, president of the Hip Hop Caucus, said before the protest. "Can you imagine during 9/11, the thousands who fled on foot to the Brooklyn Bridge, not because they wanted to go to Brooklyn, but because it was their only option? What if they had been met by six or eight police cars blocking the bridge, and cops fired warning shots to turn them back?"<p>
The blocking of the bridge took some time to emerge in the media, but it has since been documented by newspapers, magazines and television news reports--becoming one of the symbols of the racist response to the disaster.<p>
"Before we were close enough to speak, they began firing their weapons over our heads," Larry Bradshaw and Lorrie Beth Slonsky, two Bay Area residents trapped in the city by Katrina, wrote in one of the first accounts of the confrontation, published initially in <i>Socialist Worker.</i> "This sent the crowd fleeing in various directions. As the crowd scattered and dissipated, a few of us inched forward and managed to engage some of the sheriffs in conversation...<p>
"They responded that the West Bank was not going to become New Orleans, and there would be no Superdomes in their city. These were code words for: if you are poor and Black, you are not crossing the Mississippi River, and you are not getting out of New Orleans."<p>
Ron Daniels hopes that the Gretna march this week is only the first step in a wider struggle. "The intolerable acts that have been heaped on Black and poor people and people of color have been tolerated for far too long without direct action, civil disobedience and aggressive responses," Daniels said. "This is an aggressive response, and we hope that there will be many of them."<p>
"We have to draw the line in New Orleans," he said. "It's happening all over the country, but New Orleans is the visible symbol now, and we have to confront it, and make sure that New Orleans is not turned into Disneyland."<p>

