<font face="Times New Roman, Times, serif" size="4"><b>Stop the Klan</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Cindy Beringer</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AUSTIN, Texas--When the KKK came to town, Austin Mayor Will Wynn declared a day of tolerance--for hate--and urged people to ignore the bigots. Fortunately, Austinites ignored the mayor. With about two weeks' notice, several groups began organizing against the Klan's plan to rally in Austin.<p>
Fewer than 15 Klan members, who claimed to be from San Angelo in West Texas, secured a permit to rally at Austin City Hall last weekend in support of Proposition 2, an anti-gay marriage amendment proposal to the Texas constitution. About 3,000 antiracist protesters showed up to counter them.<p>
Two hundred cops in riot gear, including a SWAT team, kept protesters at least 50 yards away from Klan members carrying Confederate flags and flags bearing swastikas. Snipers were poised on the roofs of nearby buildings, and police boats patrolled under the bridge where protesters gathered. Police helicopters flew overhead. Police closed streets and bridges around the city hall, making it difficult for protesters to gather and surely lowering the antiracist turnout.<p>
Nevertheless, angry people filled the bridge across from the Klan and at least two other intersections at various times. Chants of "Bigots go home!" more than drowned out the KKK's loud-speaker system. Their "rally" was cut to about 45 minutes.<p>
The recent session of the Texas legislature failed to pass a court-mandated school finance bill put managed to put Gov. Rick Perry's pet amendment against gay marriage on the November ballot. The do-nothing governor has been courting Texas "Christian" hate denominations for support in a tough upcoming primary. Politicians and rabid Christians supporting Proposition 2 found themselves in an uncomfortable bed with their Klan allies and no real way to kick them out.<p>
Hopefully, this will give amendment supporters pause at the polls. Although many protesters came more to protest the Klan's bloody history than to denounce the amendment, the message of their chants was clear: "Gay, straight, Black, white; same struggle, same fight."<p>

<font face="Times New Roman, Times, serif" size="4"><b>Stop the execution of Stan Tookie Williams</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Alison McKenna</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SANTA MONICA, Calif.--On November 5, some 35 community activists gathered in front of Gov. Arnold Schwarzenegger's restaurant, Schatzi, to demand clemency for death row prisoner Stan Tookie Williams.<p>
Williams, who cofounded the Crips street gang at the age of 17, became an outspoken opponent of gangs while in prison, writing a series of award-winning children's books that warn against gangs, crime and prison. This work earned him a series of Nobel Prize nominations.<p>
Despite Tookie's amazing story of redemption, an execution date of December 13 has been set. But activists are turning up the heat, demanding clemency for Tookie and a moratorium on all executions. Groups who attended the protest included the Campaign to End the Death Penalty, Venice 2000, Death Penalty Focus, the International Socialist Organization and Community Call to Action.<p>

