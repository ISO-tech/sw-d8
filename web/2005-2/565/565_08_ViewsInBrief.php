<p><a name="RP"></a><font face="Times New Roman, Times, serif" size="4"><b>Why they ignored Rosa Parks</b></font><p>

<font face="Times New Roman, Times, serif" size="3">LAST WEEK, our movement lost a true hero in the person of Rosa Parks. The hidden story behind her many years of activism against Jim Crow is perhaps more important for us to hear today than at any other time since the 1955 Montgomery Bus Boycott. <p>
And yet the mainstream media devoted its blithering news coverage to the withdrawal of Harriet Miers' nomination to the Supreme Court.  To think that Miers, who will never accomplish anything of merit in her life, trumps covering a woman who helped tear down segregation! As one classmate explained to me, "That's because Rosa Parks was a Black woman who defied the status quo." How sick our world is.<br>
<b>John Green,</b> Hayward, Calif.<p>

