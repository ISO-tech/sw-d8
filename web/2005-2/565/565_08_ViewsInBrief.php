<p><a name="Struggle"></a><font face="Times New Roman, Times, serif" size="4"><b>A new struggle against racism</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SIMON WALKER seems to suggest that I argued that Black people should shun self-help strategies and wait for "racists to heal themselves" ("Cosby wasn't blaming victims," October 28). <p>
Mr. Walker apparently missed the entire point of my argument. I never called for anyone to wait for anything. Instead, I was calling for a new movement, a new struggle against the racism. <p>
The question is much clearer in hindsight. Would Mr. Walker take seriously a wealthy Black man during slavery who, from his lofty perch, loudly advocated self-help for slaves and yet was quiet on the need to abolish slavery itself? Of course not--because the primary responsibility for poor conditions in that case clearly lies with the system of slavery, not with individual slaves. <p>
I believe that today, while the "system" has changed, the need for structural change has not. The very structure of our society is racist, and we need a social struggle to transform it. The problem is that today, the "leading" (which often means wealthy) Blacks, however philanthropic, at best no longer see the need for such a struggle, and at worst outright oppose it. Given Cosby's financial "perch" and his silence on racism, I can't give him the benefit of the doubt. <p>
And for the record, I would never advocate that anyone "wait" for "profit to be taken out of the slums." In the short term, we need to build movements for affordable housing, decent wages and working conditions, and so on. These are real "self-help" strategies that can make our lives better immediately. Individual strategies will only work for a lucky minority, never for the majority. <p>
After the gains of the civil rights movement have been largely erased, it's no wonder that people like Mr. Walker don't look to a social struggle as a solution. Mr. Walker asks, "Is there any progress anywhere" on these issues? Sadly, there is very little. Even in New Orleans, where international attention has been focused on the horrible conditions of the Black population, the changes proposed are likely to make their lives worse, not better. <p>
But would Mr. Walker have dismissed the need for a movement in Montgomery in 1954, before the Montgomery Bus Boycott? Would he have said, "Where is progress on this desegregation issue?" The need for collective struggle may be difficult to see at the moment, but it remains the only hope for our short-term and long-term futures.<br>
<b>Brian Jones</b>, New York City<p>

