<font face="Times New Roman, Times, serif" size="4"><b>Another name on their list</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I RECENTLY attended a vigil in my town to mark the death of the 2,000th soldier killed in Iraq. At the vigil, they read the names of the 46 Wisconsin soldiers who have died. On the list was a name I recognized.<p>
Shane O'Donnell was fairly typical of the young men and women who do the fighting and dying in wars and occupations. He was killed a year ago. He was 24, from a small town in Wisconsin. He played football in high school and recently graduated from a plumbing apprenticeship program at our college. <p>
To honor him, his family and friends set up a fund in his name at our school to help promote the trades to Wisconsin high school students.<p>
The majority of the soldiers in the Iraq, as in all such actions, come from the ranks of the working class. They don't have much say in why and how wars are waged, but they go anyway, some out of what they've been convinced is duty, others because it is one of few options they have to earn money for college or to support their families. And when they come back--if they come back--they are often forgotten by the government bureaucrats who sent them.<p>
We need to work for a new society where we have a say in what happens to our young people, where wars are no longer waged to line the pockets of the powerful minority at the expense of everyone else. If we don't, we'll continue to hear the knock on the door, the name on the list. <br>
<b>Robin Gee</b>, AFT Local 3872, Madison, Wis.<p>

