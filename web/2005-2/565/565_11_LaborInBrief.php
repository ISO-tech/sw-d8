<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">November 11, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>New York City teachers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Megan Behrent, United Federation of Teachers</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--After more than two-and-a-half years without a contract, members of the United Federation of Teachers (UFT), the union that represents more than 100,000 educators in New York City, voted last week to ratify a contract that contains multiple concessions by a vote of about 60 percent.<p>
The contract agreement, which has sparked mass anger in schools across the city, provides for wages increases of about 15 percent over four-and-a-half years with massive givebacks that include a longer workday, a two- or three-day increase in the school year, the loss of the right to grieve letters and greater power for principals and school administrations in assigning teachers to onerous duties such as hall patrol and lunch duty. The contract also sells out new teachers hired next year--they will not receive the full raise and thus start at a lower salary. <p>
In the past few weeks, a vibrant and angry "vote no" campaign developed as teachers organized pickets at the delegate assembly and outside union headquarters in protest. Many teachers saw the contract as an insult and an attempt to weaken the union by giving up many protections related to work conditions in exchange for a raise that barely keeps up with inflation. <p>
This is why 40 percent of teachers voted "no" on the proposed contract--a significant opposition, especially in comparison with the last contract that was ratified with more than 90 percent approval.<p>
The large "no" vote demonstrates widespread anger--and the potential for rebuilding the rank-and-file militancy that's necessary to fight for real raises and improved working conditions and put a stop to concessionary bargaining. Those who voted in favor didn't do so with enthusiasm but rather out of fear and demoralization. <p>
Union leaders spent weeks trying to mobilize support for the contract by telling members that it was the best we could get. And if we voted "no," they said that we would either have to wait four more years until a new mayor was in office to get a raise or go on strike immediately without making the necessary preparations.<p>
In this context, it is understandable why many members voted for the contract--despite widespread anger at the givebacks it contained. They feared that the union under its current leadership simply could not launch an effective fight to win a better deal. <p>
Already many members of the opposition are looking ahead to the chapter elections this spring and union-wide elections next year as an opportunity to rebuild an opposition and a stronger union that can take on the union-busting strategies of politicians like Mayor Michael Bloomberg and Chancellor Joel Klein.<p>

