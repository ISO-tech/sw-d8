<font face="Times New Roman, Times, serif" size="4">Pushed off the ballot in Brunswick, Ga.</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why they fear Elaine Brown</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 11, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">SUPPORTERS OF Elaine Brown--the former Black Panther leader and candidate for mayor of Brunswick, Ga., representing the Green Party--are fighting a last-minute battle for her right to appear on the ballot.<p>
As <i>Socialist Worker </i>went to press, a superior court had denied Brown's appeal to be listed on the ballot as a candidate in the November 8 mayoral election, siding with an earlier Glynn County Board of Election ruling claiming that Brown had not established residency for long enough. Brown applied in federal court for a temporary restraining order to delay the election.<p>
The charges against Brown--who was the first candidate with enough signatures to qualify for the ballot--were brought forward by allies of her right-wing opponent, Bradford Brown. The Republican, who advertises his title as a lieutenant colonel in the Sons of Confederate Veterans, is the current mayor of New Brunswick, a city where more than half the population is Black.<p>
"The heightened sense of awareness of the voters here has never been this great," Elaine Brown's campaign manager Anita Collins told <i>Socialist Worker. </i>"She has surfaced issues that are as obvious as the hand on your arm. "We are up against the wealth of the powers that be in this area, primarily the wealthy who live on St. Simons and Sea Island who don't even vote in this election, but who have property and who have made deals that would eliminate the Black vote in Brunswick. <p>
"Before this, their candidates won office because the Black voters have chosen not to show up. Elaine has made it clear in her campaign that it would not be business as usual, and Blacks would not be disenfranchised. We would have a voice and a sense of power."<p>
The challenge to Brown's candidacy came in late September, timed to make it too late for her to qualify as a write-in candidate, Collins explained.<p>
Despite this, if Brown doesn't make it on the ballot, supporters say they're going to write her in anyway. "We are not backing down," said Collins.<p>



