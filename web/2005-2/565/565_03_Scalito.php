<font face="Times New Roman, Times, serif" size="5"><b>The right wing's dream nominee</b></font> <p>
<font face="Arial, Helvetica, sans-serif" size="2">November 11, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THE BUSH administration is charging ahead with the Supreme Court nominee that conservatives have been dreaming of. Bush will have to wait for Senate hearings on Samuel Alito's confirmation to begin in January--too late for him to rule in an important abortion case involving parental notification slated to begin in late November. <p>
But for conservatives, Alito is worth waiting for. He's already known for his anti-choice opinion in a 1991 Pennsylvania abortion case, in which he stood up for the husband's right to tell his wife whether she could have an abortion.<p>
Forget it if you're a fan of civil liberties or the right to a fair trial. In a case involving police officers strip-searching a mother and her 10-year-old daughter, Alito saw his way to favoring the cops. And when a Pennsylvania death row prisoner complained that he had received inadequate counsel in his life-or-death trial, Alito ruled against the condemned man--a decision that was narrowly reversed in the Supreme Court.<p>
No wonder the Christian Right and other social conservatives are delighted with Alito. But to Bush's friends in Corporate America, Alito's past rulings showing his inclination for rolling over workers' rights are no less important.<p>
In 1997, Alito decided against a plaintiff in a racial discrimination case, arguing that she hadn't shown enough evidence to be entitled to a trial. In 1996, he was the only dissent in the case of a female hotel employee who was the victim of sexual harassment. Here again, Alito argued that more proof was needed. <p>
In 2000, Alito ruled against a Pennsylvania state employee fired after taking unpaid sick leave. At the time, Alito said Congress had gone "too far" in passing the Family and Medical Leave Act, which guarantees 12 weeks of <i>unpaid </i>leave to care for a sick loved one.<p>
Alito makes a nice addition to Bush's coterie of pro-business, anti-union appointees--like his anti-worker, anti-union "labor secretary" and his school-shredding, teacher-hating "education secretary."<p>
But whether the Bush team--in the White House, at the Capitol, and on the Supreme Court--gets away with its right-wing agenda depends on what ordinary people do. Anger at the Bush administration--from the lies it told to go to war in Iraq to its criminally negligent behavior after Hurricane Katrina--is being expressed in all kinds of ways. Bush's continuing nosedive in the polls reached a new low again last week when a <i>Washington Post/</i>ABC News poll showed that 60 percent of people disapprove of his performance.<p>
This widespread discontent is in sharp contrast to the conservative agenda being pushed in the halls of Congress and the White House. We have to look for every opportunity to organize to start pushing back against the Bush offensive.<p>

