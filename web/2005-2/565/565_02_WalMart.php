<font face="Times New Roman, Times, serif" size="4">Cutting back on health care</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>How low will Wal-Mart sink?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 11, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">WAL-MART'S cheapness in wages and employee benefits is legendary. But the company's executives are coming up with new ideas to sink even lower.<p>
In an internal memo recently leaked to the press, M. Susan Chambers, Wal-Mart's executive vice president for benefits, expressed dismay over the growing cost of employee health care benefits. To solve the problem, Chambers suggested hiring even fewer full-time workers, discouraging the elderly or people with health problems from taking jobs at the company, and reducing corporate contributions to employees' 401(k) retirement plans. The changes, if approved, would save the company an estimated $1 billion a year by 2011.<p>
Wal-Mart is one of the most profitable companies in the U.S., with sales of $285 billion and profits of $10.5 billion last year alone. But the memo suggests even more could be made--by squeezing workers harder.<p>
In fact, in the memo, Chambers griped that longer-term workers are bad for profits. "[T]he cost of an associate with seven years of tenure is almost 55 percent more than the cost of an associate with one year of tenure, yet there is no difference in his or her productivity," Chambers explained bluntly. "Moreover, because we pay an associate more in salary and benefits as his or her tenure increases, we are pricing that associate out of the labor market, increasing the likelihood that he or she will stay with Wal-Mart."<p>
While the plan to scale back on benefits isn't yet a done deal, the memo stated that three of the company's top executives had "received the recommendations enthusiastically."<p>
Wal-Mart employees have already been squeezed to the limits. According to the <i>New York Times</i>, fewer than 45 percent of Wal-Mart's 1.3 million U.S. workers receive company health insurance--and some 46 percent of the children of Wal-mart workers are either uninsured or covered by Medicaid.<p>
Last year, 38 percent of Wal-Mart workers--who earn, on average, just $17,500 a year--spent more than one-sixth of their salaries on health care. Yet Chambers told the <i>Times </i>that Wal-Mart's "benefit plan is known today as being generous."<p>

