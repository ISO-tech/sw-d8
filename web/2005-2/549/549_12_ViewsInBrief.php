<font face="Times New Roman, Times, serif" size="4"><b>U.S. media ignores truth</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CAUGHT WITH their pants down by the international press, the corporate-controlled U.S. media is unifying around a common defense of their refusal to report on the Downing Street Memo.  <p>
The party line was first voiced by CNN, which argued a week ago that the memo simply says war is inevitable--something the domestic press had repeatedly talked about at the time. On June 14, Todd Purdam of the <i>New York Times</i> took the same tack, as did David Sanger (also of the <i>New York Times</i>) and Michael Kinsley (editorial and opinions editor of the<i> Los Angeles Times</i>) the following day. Kinsley's op-ed is titled, "Downing Street Memo is a Big Fat Dud."<p>
The point of the memo, of course, is quite different. It documents a deliberate effort to fix "the intelligence and the facts" in order to manipulate the public into accepting large-scale, unjustified murder. <p>
Having once told the American public that the Bush administration would like to invade Iraq does not absolve our supposedly independent media from their duty to report that the president was dissembling in order to make it happen.<br>
<b>Abbott Ikeler,</b> Cumberland, R.I.<p>

