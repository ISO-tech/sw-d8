<p><a name="Visteon"></a><font face="Times New Roman, Times, serif" size="4"><b>Visteon deal is positive</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I HAVE never read a story so poorly researched in my life ("Ford-Visteon deal marks new defeat for UAW," June 10). There is very little truth in it at all. <p>
Not only will we remain Ford employees (the majority of us are not Visteon employees) but the lower-paid Visteon workers will become Ford employees and double their wages and benefits when this deal passes. We will remain on the Ford payroll with full benefits when a plant is sold. We have the option to transfer to Ford facilities as positions become available if we so desire. <p>
The United Auto Workers did not suffer a defeat, nor did they give any concessions. The contract was not renegotiated. Contracts are negotiated yearly with health care providers. When they raise their prices some of the cost is reflected in co-pays. We can stay with that company or move on to another. <p>
We have many choices. Get your story straight before publishing an article. <br>
<b>Kathy Freeman, </b>UAW/VOS Coordinator, from the Internet<p>

