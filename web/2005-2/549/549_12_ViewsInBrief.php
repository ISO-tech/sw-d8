<p><a name="Guy"></a><font face="Times New Roman, Times, serif" size="4"><b><i>Family Guy</i> isn't funny</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I WAS appalled to read that Sharon Smith had a favorable review of Fox's <i>The Family Guy</i> ("Just your ordinary all-American family," June 10). This has been a longstanding disagreement with my spouse, who also thinks the show is a slice of satirical heaven. <p>
One episode they couldn't air before the show was canceled the first time (but can now be seen regularly on cable), "When You Wish Upon a Weinstein," is racist, pure and simple. It is a prism though which every single episode can be seen. You cannot tell if we're supposed to be skewering racism against Jews or laughing and agreeing they sure are a peculiar, money-grubbing bunch. <p>
Despite the opening number on the show, which Smith describes, it's unclear what <i>The Family Guy</i> is saying or why. No line is consciously drawn for the audience as to whether this humor should be taken as left or right wing, which if there was, I would see and agree with everything that Smith says. <p>
It "pokes fun at everyone" as if we're all on the same playing field--when all of us don't deserve to be poked fun at, and I feel it uses the working class to mock the working class. Praise of <i>The Family Guy</i> has no business in our paper.<br>
<b>Amanda Maystead,</b> San Francisco<p>

