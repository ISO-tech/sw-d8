<p><a name="Are"></a><font face="Times New Roman, Times, serif" size="4"><b>Are Americans conservative?</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CARL DAVIDSON justifies shifting to the right by saying "most of the folks we have to win to oppose the war...are to the center and right of where the antiwar movement is" ("Are the Democrats our allies?" May 13). His attitude reveals one of the ugly truths of U.S. political culture: While conservative complaints about liberal "elitism" are hypocritical, they are also substantially correct.<p>
Conservatives have never wavered in their belief that they can win "ordinary Americans" (whoever they are) to their point of view. For decades, conservatives have organized in churches and through mass mailings; they have held rallies and even engaged in direct action (e.g., Operation Rescue's blockading of abortion clinics). Their tireless dedication to grassroots organizing has paid off handsomely; the spectrum of "mainstream" opinion in the U.S. today is narrower and more skewed to the right than at any other time since the 1950s. <p>
While conservatives have been fortifying their base, liberals have all but abandoned their commitment to building resistance from below. Apparently, liberals have decided that most "folks" are just too stupid, lazy and/or reactionary ever to be won over to radical politics. <p>
As a result of this elitist attitude, liberals have increasing relied on backing "friendly" Democrats, while trying to sideline issues like gay marriage, which they feel red-state "rubes" are too dumb and hated-filled to support. <p>
I am sorry to see that liberals, not radicals, presently dominate the antiwar movement. Were that not the case, the antiwar movement would be much stronger.<br>
<b>Dennis Fritz,</b> Laredo, Texas<p>

