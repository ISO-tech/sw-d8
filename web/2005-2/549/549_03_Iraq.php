<font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Even fellow Republicans are criticizing Bush's occupation</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Iraq's downward spiral</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 24, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THE BUSH administration's latest attempts to "reassure" Americans that Iraq is on the road to democracy and stability have only served to highlight the crisis of the U.S. occupation.<p>
Hammered by opinion polls that show public support for the occupation falling to new lows--and six in 10 people in favor of full or partial withdrawal of U.S. troops immediately--the Bush administration is reportedly scheduling new photo ops and stage-managed events to "regain the initiative." The p.r. campaign is expected to build toward June 30--the first anniversary of the fake handover of power to an interim Iraqi government.<p>
As before, the administration hopes to bluster its way through, with tough rhetoric and the support of a sympathetic corporate media. But the truth is getting hard to hide--so much so that even Republicans are publicly chastising the administration.<p>
"Things aren't getting better, they're getting worse," top GOP Sen. Chuck Hagel told <i>U.S. News and World Report</i>. "The White House is completely disconnected from reality. It's like they're just making it up as they go along. The reality is that we're losing in Iraq." <p>
There's no shortage of evidence to back up Hagel's conclusion. What administration officials insist on describing as a "spike" or "peak" in violence in Iraq has continued for months--pushing the death toll for U.S. soldiers past 1,700 and leaving dozens of Iraqis dead every day in new attacks.<p>
The new government put in office by the January 30 election has stumbled from one crisis to the next. Its main job was supposed to be drafting a new constitution. But even supporters of the U.S.-backed regime admit that there is a long way to go before the deadline of August 15, less than two months away.<p>
Meanwhile, the U.S.'s divide-and-conquer tactics--employed increasingly in the run-up to the January elections--have deepened ethnic divisions and created the conditions for civil war.<p>
The latest case in point: news reports suggesting a campaign of ethnic cleansing carried out by Washington's most stable ally in Iraq--leaders of the minority Kurds. According to the <i>Washington Post, </i>Iraq's two leading Kurdish parties used their police and security forces in the northern city of Kirkuk to kidnap hundreds of Arabs and Turkmen--at least some of them in "joint-U.S.-Iraqi raids," the <i>Post </i>reported.<p>
Kirkuk--an ethnically divided city of 1 million on the edge of Kurdish strongholds in the north--is described even by U.S. officials as a "powder keg." It also happens to sit on top of one of Iraq's richest oilfields.<p>
According to the <i>Post, </i>the Kurds' victims have been held in prisons outside any legal framework. "The detainees," the paper wrote, "have often remained missing for months; some have been tortured, according to released prisoners and the Kirkuk police chief."<p>
Such stories--found more consistently in the international media than the lapdog American press--underline the yawning gap between the image of the occupation that the administration would like to promote, and the grim reality.<p>
The crisis has grown to such proportions that even Republican Reps. Walter Jones and Ron Paul have joined Democrats and liberal darlings Dennis Kucinich and Lynn Woolsey in sponsoring legislation calling on the Bush administration to set a timetable for withdrawal.<p>
This is a sign of growing unease within the U.S. ruling establishment. But the antiwar movement must recognize that politicians who talk about setting a "timetable for withdrawal" are really proposing a continued occupation for at least months and probably years--until a pullout is deemed "responsible."<p>
This is not far different from the position of the Bush administration: that the U.S. will withdraw as soon as possible, but not before a new government and security apparatus--pliant to Washington's interests, of course--is in place.<p>
Unfortunately, representatives of Iraq's only legal union--the Iraqi Federation of Trade Unions--are promoting a similar line at meetings in U.S. cities organized by U.S. Labor Against the War. Though the IFTU members claim to be anti-occupation, they insist, as one said at a meeting in Baltimore that "we must set up the necessary political structures and establish security" before the U.S. withdraws.<p>
In reality, the force responsible for chaos and "terror" in Iraq is the U.S. military. Every day longer that the U.S. occupies Iraq is another day of violence and injustice imposed on the Iraqi people.<p>
Antiwar activists at the grassroots can begin building now for a huge turnout for the national mobilization in Washington, D.C., on September 24--where we can restate in stronger terms our commitment to the immediate withdrawal of all U.S. troops.<p>

