<font face="Times New Roman, Times, serif" size="4">Three-day strike sends message to University of California</font> <br>
<font face="Times New Roman, Times, serif" size="5"><b>Clericals strike UC system</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Martin Smith, CUE Local 10</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 24, 2005 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">THOUSANDS OF Coalition of University Employees (CUE) members began a three-day strike at all 10 campuses of the University of California (UC) June 14 to demand higher wages. It was the third strike to challenge UC's administration in just over two months.<p>
CUE represents more than 16,000 clericals, library assistants, child care workers, cashiers and safety dispatchers--critical positions the university could not function without but that are undervalued by the administration. The majority of CUE members are women and people of color, and it is no surprise that they are among the worst paid at UC.  <p>
In the 2003-04 academic year, UC promised $30 million to increase clerical wages. But when the state of California refused to provide $10 million to this end, the university decided to divert its $20 million to expand dormitories and parking facilities. <p>
UC has flagrantly fanned the flames of workers' anger by giving massive wage increases to top administrators. Last year at UC-San Diego, the regents appointed a new chancellor at $350,000--nearly 25 percent more than the last chancellor was paid--and UC Treasurer David Russ recently received a 61 percent raise for a total base salary of $450,000. Meanwhile, the university pleads poverty when confronted with the demand to increase clericals' wages. <p>
At a rally at UC-Santa Cruz during the strike, CUE member Osiris Ortiz tearfully explained that her parents had sold their home to buy her a car so that she could commute back and forth to work, because she cannot afford the high rental costs in the city of Santa Cruz.<p>
In San Diego, more than 450 workers picketed at two locations in front of medical centers run by the university.  <p>
The rallies were spirited and exposed the anger felt by UC's clericals, who feel ignored and disenfranchised. Solidarity between the various unions that represent UC workers, as well as the support of students, is key to winning a decent contract for clerical workers.<p>
Unfortunately, CUE, AFSCME and the University Professional and Technical Employees have all held separate strike actions even as they each try to negotiate new contracts. What's more, many members of CUE have expressed frustration at the lack of planning that has led to a strike for only three days and after the school year ended when their leverage is diminished.  <p>
After having zero raises during the last few years, many UC clerical workers are ready to go on an extended strike. This anger must be organized in solidarity with other campus workers as well as students.<p>
<i>Rebecca Anshell contributed to this report</i>.<P> 

