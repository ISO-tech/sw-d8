<font face="Times New Roman, Times, serif" size="5"><b>A non-debate about labor's future</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 24, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">YOU KNOW the debate on labor's future is totally scrambled when a strikebreaking union bureaucrat starts invoking the struggles of the militant Congress of Industrial Organizations (CIO) of the 1930s.<p>
United Food and Commercial Workers (UFCW) President Joseph Hansen is the latest union chief to weigh in on the side of the labor leaders grouped around Service Employees International Union (SEIU) President Andrew Stern in advance of next month's AFL-CIO convention. Both unions are threatening to leave the federation.<p>
"[I]n the 1930s, we were at our lowest point, but also on the verge of our greatest accomplishments," Hansen said in an open letter announcing the formation of the "Change to Win" coalition with Stern and other labor leaders. "When the CIO left the AFL in the 1930s, it did not set us back, it propelled the movement forward."<p>
That's really something coming from a union official who made his debut on the national scene in the 1980s by wrecking a strike at the Hormel meatpacking company's flagship plant in Austin, Minn. It was Hansen who orchestrated a union trial to remove the leadership of UFCW Local P-9 in Austin--and then negotiated a concessionary contract over the heads of union members.<p>
Hansen and other UFCW officials insisted that this unpleasant strategy was necessary for the union to establish a new--lower--wage level across the meatpacking industry that could then be built upon for future gains. Instead, union power in the industry--which once boasted some of the highest wages for blue-collar work--completely unraveled in the wake of the P-9 defeat.<p>
Hansen has been at it again more recently, this time in the grocery industry. Taking over as union president following the UFCW's disastrous conduct of last year's Southern California grocery strike, Hansen pushed a series of concessionary contracts--necessary, he says once again, to hold the line.<p>
Hansen's current alliance with Stern--whose other main ally is Teamsters President Jimmy Hoffa--highlights the fact that this new alliance doesn't offer much of a coherent strategy to revive the unions.<p>
Rather, the Stern-Hoffa-Hansen bloc's main focus is on downsizing the AFL-CIO federation by rebating more dues money back to affiliated unions for organizing the unorganized. Another hallmark of the Change to Win coalition is a call for union mergers--not along the industrial lines originally proposed by Stern last year, but as a simple big-is-best strategy in the hopes of maintaining the unions' continued viability.<p>
The other side in labor's debate doesn't offer an alternative. It consists of stand-pat conservatism from unions like the International Association of Machinists and United Steelworkers of America.<p>
Both sides of the debate advocate concessions and "partnership"--rather than struggle--to shore up relationships with employers. For example, the United Auto Workers--which backs AFL-CIO President John Sweeney against Stern's coalition--has accepted higher health care costs imposed by Chrysler, the elimination of thousands of jobs by Ford's former parts maker Visteon and further givebacks at General Motors and its former parts subsidiary, Delphi.<p>
These concessions will be a powerful downdraft on wages in manufacturing--at a time when wage growth has already slipped behind the rate of inflation, despite an economic recovery.<p>
Labor's non-debate about its future hasn't addressed this crisis at all--and vague references to labor's upsurge in the 1930s won't change that fact. A revival of the unions will ultimately come not from reorganization at the top, but--as with the CIO--a revolt from below.<p>

