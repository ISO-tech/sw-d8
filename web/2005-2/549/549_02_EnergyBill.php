<font face="Times New Roman, Times, serif" size="4">Rip-offs and giveaways in the White House energy bill</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Helping hand for power bosses</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 24, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">GEORGE W. Bush had a warning for Senate members debating the details of a broad energy bill in the coming weeks. "My advice is they ought to keep this in mind: Summer is here, temperatures are rising, and tempers will really rise if Congress doesn't pass an energy bill," threatened Bush.<p>
Last week, Democrats and Republicans were setting the stage for a raging debate to come.<p>
Sen. Lamar Alexander (R-Tenn.) went ballistic over the passage--by a narrow 52-48 margin--of an amendment to the bill that would require major electric companies to obtain 10 percent of their electricity from clean renewable energy sources such as wind, solar and bioenergy by 2020. "I'm not anxious to go home to Tennessee and say, 'We are worried...that natural gas prices are high, and our solution: Tens of thousands of windmills.'"<p>
But windmills certainly aren't the only form of energy "alternatives" being proposed. Senators are also discussing increased spending to research so-called "clean-coal" technology--and incentives to build nuclear power plants.<p>
The Senate is due to vote on its version of the energy bill this month--the House passed its version in April. In July, a conference committee is supposed to work out the differences.<p>
Last year, the House and Senate passed energy bills, but final legislation was stalled in part over a fight over methyl tertiary-butyl ether (MTBE), an additive to gasoline made from natural gas. MTBE, which causes cancer, has seeped into water supplies in all 50 states through leaking storage tanks. House Republicans want language in the energy bill that would protect MTBE sellers, like oil giant ExxonMobil, from lawsuits.<p>
This time around, the Senate version of the bill will likely not even take up MTBE. Nor will it take up another deal breaker the last time--oil and gas drilling in Alaska's Arctic National Wildlife Refuge (ANWR). Instead, the plan is to include legislation allowing ANWR drilling in a final budget measure this year.<p>
If one thing is certain about the outcome of the energy bill, it's the billions in tax breaks that Bush's pals in the energy industry will pocket. And Democrats and Republicans alike are making sure that Corporate America gets its piece of the pie.<p>
In a bipartisan giveaway to agribusiness, the Senate looked certain to approve requiring the use of 8 billion gallons a year of federally subsidized ethanol--a corn-based gasoline additive--by 2012. That's about double the amount of ethanol currently used.<p>
Ultimately, questions like global warming and greenhouse gas pollution will go unaddressed in this energy bill. The Bush administration refuses to admit that global warming is being caused by the emission of carbon dioxide and other greenhouse gasses produced by burning fossil fuels.<p>
Meanwhile, the Democrats are hardly coming up with any solutions. In what is looked upon as the most sweeping proposal, Sen. Joe Lieberman (D-Conn.) joined forces with Sen. John McCain (R-Ariz.) to come up with a plan to cut U.S. carbon output to 2000 levels by 2010. <p>
But their plan hinges on offering incentives to build nuclear power plants--and setting up a trading program so that corporations can buy and sell carbon "allowances." In other words, corporations will be able to buy and sell their way out of pollution controls.<p>
Despite the grandstanding, the energy bill will be a boon for the energy industry, no matter what the cost is to the environment.<p>


