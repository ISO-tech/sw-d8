<font face="Times New Roman, Times, serif" size="5"><b>A cog in the Bush war machine</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 24, 2005 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">DAO TRAN is a member of the International Socialist Organization in New York City and a contributor to <i>Socialist Worker. </i>Here, she writes about her sister, who's an Army nurse.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">MY SISTER shipped out to Baghdad in early June. Besides worrying and fearing for her safety--horrified that she is being thrust into the midst of a war zone--I find myself wrestling with a myriad of other issues. <p>
She is a head nurse with a mobile hospital unit. She's also, like me, of Vietnamese descent. She joined the Army voluntarily many years ago and has always seen it as a stable and smart career move. Besides, she wanted to travel, and it had the additional "benefit" of getting her out of a tense family situation.<p>
She tells me that she supports the war and thinks it's her duty to go--and nonchalantly adds that she's not too concerned because she'll be in the fortified Green Zone compound in the heart of Baghdad. I think to myself that her support for the war won't keep her safe, no matter how supportive of the right wing she is. Last time I checked, Bush and Rumsfeld do not make the protection and safety of soldiers a high priority. <p>
Up until last week, she's been putting in 12-hour days at Walter Reed Hospital in Baltimore and is stunned and exhausted by the number of injured and broken returning from Iraq. She jokes that she has to go to Iraq to get some rest. At the end of the conversation, she confides that she's "a little nervous" and just wants to come back in one piece.<p>
I can't express how absurd it is that someone who went into nursing--an occupation that's about saving lives and taking care of people--is now going to become a cog in the Bush-Exxon-Halliburton war machine. <p>
It is further insanity that she was a 7-year-old girl in Vietnam the <i>last</i> time the United States invaded another country. How did she end up in the Army of the same government that killed millions of Southeast Asians, destroyed countless lives and caused my own family to flee to the U.S? <p>
I can't help but think that part of the reason why she doesn't see this paradox is the absence of a principled and active antiwar movement in this country. That movement could have pointed out why it didn't make sense to join the Army as a career opportunity or the real reason--the drive for oil and empire--behind the U.S. drive to invade and occupy Iraq, <p>
At its height, the anti-Vietnam War movement and the anti-imperialist environment it created made it common sense that no child of Vietnamese refugees would be in the U.S. Army. <p>
Sometimes it takes experience to learn this. My brother was in the Navy during the 1991 Gulf War. He, fortunately, didn't go to Iraq. But it changed him profoundly. By the end of his four years, he was cynical about the U.S. as a force for world peace, fed up with the racism within the Navy, and convinced that "NAVY" was an acronym for "Never Again Volunteer Yourself." <p>
I hope that it will not take the same bitter experience for my sister to figure this out. I hope she doesn't come back badly hurt or worse. I hope she can come back to a strengthened antiwar movement that can change the context and conditions in which people like her make the "choice" to participate in the occupation of Iraq or any other war.<p>

