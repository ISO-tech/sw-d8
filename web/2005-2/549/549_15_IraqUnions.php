<font face="Times New Roman, Times, serif" size="5"><b>Iraqi trade unionists tour the U.S.</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 24, 2005 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">A TOUR of Iraqi trade unionists highlighted the terrible conditions for workers in Iraq--but also raised new questions about the pro-occupation stance of Iraq's main union federation.<p>
The tour, sponsored by U.S. Labor Against the War (USLAW), was broken down into three components: East Coast meetings by what is reportedly Iraq's main labor body, the Iraqi Federation of Trade Unions (IFTU); Midwest stops by the Federation of Workers Councils and Unions of Iraq (FWCUI); and on the West Coast, the General Union of Oil Employees (GUOE), based in the Basra oilfields.<p>
In Los Angeles, GUOE leaders Hassan Juma'a Awad and Faleh Abbood Umara spoke at three events--a luncheon with the International Longshore and Warehouse Union, a meeting with United Teachers Los Angeles and a public forum in the evening.<p>
Both speakers called for an immediate end to the occupation.  "Help us by putting pressure on your administration so that we can rebuild ourselves," Abbood Umara said.<p>
Speakers at the meeting included Fernando Suarez del Solar from Military Families Speak Out; Nicholas Przybyla, a Navy veteran from Iraq Veterans Against the War; and Arlene Inouye from the Coalition Against Militarism In Our Schools. There was also a surprise visit from U.S. Rep. Maxine Waters (D-Calif.) who had just returned from Washington, D.C.<p>
The GUOE representatives also spoke to 150 people in Berkeley, Calif., in a meeting co-sponsored by the Labor Committee for Peace and Justice and Code Pink. The two spoke about the plans to grab Iraq's oil and privatize its economy for the benefit of U.S. and Western corporations. "There is a disease which is plaguing Iraqi society, and that is privatization," Abbood Umara said.<p>
The character of the Iraqi resistance was an issue at the meeting. "The targeting of Iraqi security forces [and] car bombs are acts of terrorism," said Hassan Juma'a, opposing attacks on the Iraqi forces created and run by the U.S. "We are opposed to this terrorism."<p>
A similar question was raised at the meeting of FWCUI representatives in Chicago, where the federation's president, Falah Awan, said the legitimate right of resistance under the Geneva Convention had been taken over by "backward elements" and "terrorists" that helped lead to the creation of a police state.<p>
While this characterization of the resistance led to some debate in the meeting, there was widespread support when Awan and the North American representative of the FWCUI, Amjad Ali Al-Jawhary, called for an immediate end to the U.S. occupation.<p>
The meeting, held at the UNITE HERE hall, was cosponsored by Chicago Labor Against the War and Chicago Labor for Peace, Prosperity and Justice, both USLAW affiliates. The question of the occupation--and the IFTU's past statements in support of it--came up repeatedly on that federation's leg of the tour.<p>
The IFTU was appointed by the Governing Council and then the interim government of Iyad Allawi as Iraq's sole legal union, giving it control of the old government-controlled union's membership lists.<p>
At a meeting in Baltimore June 15, the IFTU representatives implied that the U.S. destruction of Falluja was justified because "bad people" from outside Iraq had used the city as a base.<p>
Yet at a June 17 meeting in New York at the SEIU/1199 hall, IFTU executive council member Abed Sekhi said his union's goal was to "end the occupation." He said concerns about the IFTU's position were the result of a "misunderstanding of our position." However, members of New York City Labor Against the War, which refused to endorse the event, issued a leaflet that detailed the role of the IFTU's representative in heading off an anti-occupation resolution at last fall's British Labour Party conference.<p>
The size of several events on the USLAW tour indicated the potential for antiwar organizing--but the IFTU's positions on the occupation undermined the effort to step up the pressure on the demand for an immediate withdrawal of U.S. troops.<p>
<i>Danielle Heck, Michael Hoffman, Sarah Knopp, Dennis Kosuth and Sarah Wolf contributed to this report</i>.<p>

