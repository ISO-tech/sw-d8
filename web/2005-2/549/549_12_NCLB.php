<font face="Times New Roman, Times, serif" size="5"><b>A law that leaves kids behind</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 24, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">I THINK Jesse Muldoon's recent article about No Child Left Behind (NCLB) and its destructive effect on schools was dead right ("How many schools left behind?" May 13). While the recent spate of lawsuits challenging the law are certainly welcome in attacking the hypocrisy of NCLB, the two false poles of the debate on the law have only been reinforced. <p>
The fight around NCLB shouldn't be about winning "adequate funding." Even with all the money in the world, the focus would still be on drill-n-kill testing; the Bilingual Education Act would still be deleted; the relaxation of special ed requirements would still be in place, etc. What we need is to fight to get rid of the law in the first place.<p>
Recent events here in Arizona prove my point. The standardized test for this state, called AIMS, will be a graduation requirement as of next year. That means students who don't pass AIMS don't graduate. Some 60 percent of current juniors, the ones this new rule applies to, failed the AIMS test last fall. <p>
No wonder--Arizona ranks 49th out of 51 (states plus the District of Columbia) in terms of per-pupil funding. The state spends a paltry $380 per year for each English-language learner, even though they make up a significant minority of students in this border state. <p>
Obviously, the state education department can't have upwards of two-thirds of its kids not graduate. Their plan? Rig the scoring of the test to make it easier to pass, give credit for good grades to earn extra points on the test, and put stickers on diplomas of special education students so that everyone will know they were allowed to graduate without passing AIMS. <p>
Outrageously, the Arizona Education Association, the teachers' union here, is endorsing some of these quick fixes. It's critical that teachers help steer the fight not toward fixing and funding this punitive and destructive law, but toward getting rid of it in the first place and replacing it with a real educational policy that puts genuine learning for all students at its heart. <br>
<b>Jeff Bale, </b>Phoenix<p>

