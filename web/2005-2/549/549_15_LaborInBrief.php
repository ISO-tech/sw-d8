<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">June 24, 2005 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>New York University</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Mitch Day, Graduate Student Organizing Committee</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--New York University (NYU) announced in early June that it would not negotiate a second contract with the graduate assistant (GA) union when its contract expires at the end of August.  <p>
This union-busting came as no surprise to members of the Graduate Student Organizing Committee (GSOC), which is affiliated with United Auto Workers (UAW) Local 2110. For months, NYU has been criticizing the union through mass e-mails to the university community. It set up a fake "decision-making" process that consisted of various administrator-stacked committees, with no participation of organized GAs.  <p>
NYU's official rejection of the union focuses on the grievance procedure. NYU says that it fears that continuing the current procedure "would undermine the critical role of the University's faculty in making academic judgments," according to a memo issued by NYU officials.<p>
In reality, the "judgments" that NYU doesn't want challenged have to do with cutting corners on pay. During the past contract, there were several instances where NYU filled in positions by employing graduate students as adjunct teachers, with lower pay and less benefits. These individuals recognized they were performing the same jobs as GAs and rightly looked to the union to demand equal pay for equal work.<p>
In its first contract, GSOC was able to win full health care coverage for GAs, a 36 percent increase in stipends and a grievance procedure. NYU claims it will continue providing full coverage, increase stipends over the next three years and implement its own "internal" grievance procedure.  <p>
But these are just promises meant to convince GAs that they don't need a union. Without collective organizing, there is no guarantee that NYU will follow through on these promises.<p>
With NYU's official rejection of graduates' democratic right to unionize, there is serious talk of striking in the fall. Four years ago, NYU recognized GSOC one night before the union held a strike authorization vote. With the administration again taking a hard line, it will take just as much pressure to win a second contract.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Specialty Filaments</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Erik Wallenberg</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BURLINGTON, Vt.--More than 100 people rallied outside Specialty Filaments Inc. (SFI) after its parent corporation, Capital Resource Partners Investment Firm (CRP), announced it was closing the plant and laying off 162 members of UNITE HERE Local 438.<p>
For more than 135 years, workers at SFI have been producing filaments for everything from paintbrushes to toothbrushes. One union member's sign summed up the feelings of many at the protest: "CRP has the gold mine, and we got the shaft."<p>
"They've run us into the ground, and now they're throwing us out," said Mike Green, a former chief steward who has worked at SFI for 22 years. "They say it's because of foreign competition, but the investors are taking the money."<p>
CRP manages more than $1 billion in assets. The union's demands have centered on better severance packages for workers, but to stop labor's long decline, they will have to take on these attacks more directly. As Butch, a UNITE HERE member with 34 years' experience, said at the rally, "We should do a slow down and tell [SFI] to go to hell now!"<p>

