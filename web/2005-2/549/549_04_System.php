<font face="Times New Roman, Times, serif" size="4"><b>Parachute of black gold</b></font><p>

<font face="Times New Roman, Times, serif" size="3">DON'T CRY for Philip Cooney. The former chief of staff of the White House Council on Environmental Quality resigned earlier this month, after revelations that he had edited government reports to downplay the link between pollution and global warming.<p>
Cooney's editing is part of the Bush administration's attempts to discount the threat of climate change--in order to put off curbs on the oil industry's emission of greenhouse gases.<p>
According to the White House, Cooney's rapid departure in early June had nothing to do with the breaking scandal."Mr. Cooney has long been considering his options following four years of service to the administration," a White House spokesperson told reporters. "He'd accumulated many weeks of leave and decided to resign and take the summer off to spend time with his family."<p>
Before going to the White House, Cooney was a former lobbyist for the American Petroleum Institute. Now he's returning to the hearth.<p>
According to reports last week, the oil-industry "yes man" landed on his feet. Just days after his resignation, Cooney was hired by ExxonMobil Corp.--the world's largest oil company. Exxon executives, not surprisingly, have been among the most critical of the scientific fact that climate change is being caused by pollution.<br>
-- <b>Associated Press</b>, June 15, 2005 <p>

<font face="Times New Roman, Times, serif" size="4"><b>Radio Not-So-Free Ohio</b></font><p>

<font face="Times New Roman, Times, serif" size="3">LISTENERS TUNING into Radio Free Ohio in May might have thought that they had stumbled on a "pirate" anti-corporate radio station.<p>
For weeks, the amateur-sounding signal went out over the broadcasts of stations in Akron, Ohio, owned by radio conglomerate Clear Channel. On their Web site, www.radiofreeohio.com, the "pirates" posted a manifesto condemning local Clear Channel stations and complaining about "corporate-controlled music playlists."<p>
But as it turns out. Radio Free Ohio wasn't a prank played by indie pirates. It was Clear Channel's prank on an unsuspecting public.<p>
The broadcasts were part of a new publicity campaign to drum up business for an Akron Clear Channel station that was switching from sports to a progressive talk radio format. "Once we determined we were going to change the format, we tried to get into the mindset of people who would listen to this new station," said Dan Lankford, vice president and market manager for Clear Channel in Akron. "Clear Channel, as I see it, is dedicated to entertaining radio and to getting results for our advertisers. There's a hole in the market here and we're going to fill it."<br>
-- <b><i>New York Times</b></i>, May 30, 2005<p>

