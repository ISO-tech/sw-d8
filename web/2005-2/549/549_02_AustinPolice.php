<font face="Times New Roman, Times, serif" size="5"><b>Shot in the back by Austin police</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Mike Corwin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 24, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">ANGER HAS boiled over in Austin, Texas, after police shot and killed an unarmed 18-year-old earlier this month. This is the third cop murder of a person of color since 2002.<p>
Daniel Rocha was shot in the back at point-blank range after a car that he and two friends were riding in was stopped by police on suspicion of drug selling.<p>
In the aftermath of the shooting, police officials wasted no time informing the media that Rocha had a record of petty crime. But they took days to come up with their official story regarding the shooting.<p>
Officer Julie Schroeder claims that she shot Rocha out of concern that he could have attacked another officer with a Taser stun gun missing from her belt. But this story is being challenged by eyewitnesses, who say Daniel lifted his shirt to show he had no weapons. He was then pushed to the ground, where an officer placed a knee in his back and shot him.<p>
"He was yelling, 'I'm not armed. I'm not armed,'" witness Stephanie Pesina told reporters. "And I guess there was a little bit of a struggle. All I remember is you heard a gunshot. It wasn't like a loud gunshot, where you could hear it in the air. It was like she held [the gun] up to him and shot him."<p>
No video exists of the shooting, despite the fact that police are equipped with video cameras and are required to turn them on during traffic stops. Police later claimed they failed to turn the camera "all the way on."<p>
Austin's communities of color are already on edge following a report documenting racial harassment by police, and an incident in which officers celebrated a fire at a popular nightclub in the Black community with racist slurs over the police messaging system.<p>
A few days after the shooting, 150 community activists and friends and family of Rocha rallied at Austin police headquarters. "To them, he was just another minority on the streets, a hoodlum," Aaron Hanlon, a friend of Rocha's who attended the rally, told <i>Socialist Worker</i>. "They just see the color of your skin."<p>
At a community forum a week after the shooting, hundreds heckled and railed at police officials in attendance. Many shared their own horror stories about police abuse and violence. "You want the facts?" asked one speaker. "Here are the facts: Daniel Rocha had just turned 18. He was just a kid. He was shot in the back. Somebody's baby is not coming home tonight."<p>
Community activists vowed to keep up the pressure to win justice for Daniel Rocha. The Austin police have, for the most part, gotten away with a history of murder and racism. It will take real pressure from outside the system to win justice and to stop this kind of cold-blooded murder from happening again.<p>

