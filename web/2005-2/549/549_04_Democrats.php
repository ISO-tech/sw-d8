<font face="Times New Roman, Times, serif" size="4">Polls show discontent with Bush <i>and </i>Congress</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Democrats shift into reverse</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 24, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">PUBLIC OPINION is turning against the Iraq war and the political status quo in Washington. <p>
A June Gallup poll found a clear majority--59 percent--of respondents wants a full or partial withdrawal of U.S. troops from Iraq. An NBC/<i>Wall Street Journal </i>opinion poll on May 19 revealed an "angry electorate" fed up with Washington's politics-as-usual. "The public is exceptionally displeased with the Congress," noted pollster Peter Hart.<p>
This warning was not lost on politicians holding their fingers to the wind, judging from the recent flurry of activity on Capitol Hill. Calls for an Iraq exit strategy, however tepid or timid, have been echoing through the halls of Congress.<p>
On May 28, 128 members of the House voted to support California Rep. Lynn Woolsey's non-binding amendment that the president "develop a plan as soon as practicable...to provide for the withdrawal of United States Armed Forces from Iraq." Sen. Russ Feingold (D-Wis.) introduced a similar resolution in the Senate. <p>
In another non-binding resolution, former Democratic presidential hopeful Dennis Kucinich (D-Ohio) united with North Carolina Republican Walter Jones (best known for imposing "freedom fries" on the House cafeteria in a fit of patriotic zeal in 2003), requesting that Bush start withdrawing U.S. troops by Oct. 1, 2006.<p>
Rep. John Conyers from Michigan is demanding a congressional investigation into impeachable offenses that occurred when Bush lied his way into invading Iraq, based on evidence from the now-infamous "Downing Street memo." <p>
These developments are certainly welcome after the Democrats maintained a disciplined silence as they fell into line behind pro-war John Kerry in the 2004 election. But Democratic Party powerbrokers show no sign of embracing an oppositional role. If anything, they are moving further into reverse. <p>
In an "open letter" to the party, the (Clintonite) Democratic Leadership Council (DLC) argued, "winning the war on jihadist extremism will be the Democratic Party's first priority this year and every year until the danger recedes." All the leading Democratic presidential contenders for 2008--including Sens. Joe Biden, Hillary Clinton and Evan Bayh, along with John Kerry's running mate John Edwards--have joined a faction boldly declaring itself the "National Security Democrats."<p>
As Matt Taibbi described in the <i>New York Press,</i> "The position of the 'National Security Democrats' is that the party should be 'more open to the idea of military action, and even preemption'...Additionally, the Democrats ought to reconsider their traditional stance as an opposition party and learn to embrace Republican heroes like Ronald Reagan." (Wait. Didn't Kerry already do that?) <p>
The Democratic National Committee (DNC) executive committee's June meeting also shed light on the party's chosen path.<p>
Shortly before the Iraq war, Howard Dean propelled himself to the antiwar forefront by arguing to the DNC, "What I want to know is why in the world the Democratic Party leadership is supporting the president's unilateral attack on Iraq?" <p>
More than two years into the occupation, Dean, now the DNC's chairman, didn't mention the Iraq war at all. Instead, Dean stressed the party's message should make "it easier for parents to teach their kids right from wrong."<p>
DNC executive committee member Judith Hope argued for finding "common ground with faith-based voters" by creating a task force on "violence and sexual immorality which has permeated our entertainment industry." (Hold on. Didn't Al and Tipper Gore already try that?)<p>
The party's rising star, Sen. Barack Obama (D-Ill.), chose Father's Day to deliver the Democrats' message on "teaching kids right from wrong." In a sermon to a Black congregation in Chicago, Obama ridiculed men in the Black community as too immature to take responsibility for their own actions and set a proper example for their children. <p>
Obama told worshippers, "There are a lot of folks, a lot of brothers, walking around, and they look like men...they might even have sired a child." "But it's not clear to me that they're full-grown men," he continued.<p>
The Bush administration's house of cards should be collapsing along with Bush's plummeting ratings in the court of public opinion. If it isn't, the Democratic Party is to blame.<p>

