<font face="Times New Roman, Times, serif" size="5"><b>Federal foot-dragging over mad cow threat</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 24, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">A SECOND cow in the U.S. has tested positive for mad cow disease--but the U.S. Department of Agriculture (USDA) is dragging its feet in doing anything about it. The USDA insists that further testing is necessary to determine whether the second animal is infected.<p>
Since the first cow with mad cow disease--known to scientists as bovine spongiform encephalopathy (BSE)--was found on U.S. soil in December 2003, the USDA and the cattle industry have launched a full-scale campaign to convince both foreign and domestic consumers that the U.S. beef supply is fully tested and safe.<p>
After all, they argued, the infected cow had been imported from Canada. Officials also announced that they intended to close feed ban loopholes that left U.S. cattle vulnerable to outbreaks of BSE--which spreads when cows eat brain and nerve tissue of already-infected cattle. But once the media coverage of the outbreak died down, they didn't keep their promise.<p>
Before a BSE outbreak in Britain in 1997, cattle remains that were byproducts of the slaughtering process could be used as protein in cattle feed. Britain and other countries banned such ingredients in feed--but not the U.S.<p>
For example, it's still legal to put ground-up cattle remnants in chicken feed--and to sweep up the chicken feed that spills from cages and use it (and the chicken waste it's mixed with) in cattle feed. This completes a feedback loop that allows cattle to eat the remains of other cattle.<p>
The USDA is supposed to protect consumers by insuring that the food supply is safe. In reality, this "independent government agency" is dominated by officials from the industry it is supposed to regulate. <p>
"The USDA is very closely tied to large industry groups," Diane Farsetta, senior research at the Center for Media and Democracy, told <i>Socialist Worker.</i> "The National Cattlemen's Beef Association and other groups have former staff and lobbyists in key USDA positions. The USDA is structurally biased in favor of industry, and when the agency is presented with a threat to both animal and human health, these ties affect everything about its response--from how they collect information to how they make policy."<p>
The discovery of another animal with BSE comes as a blow to industry efforts to restore beef exports to Japan and South Korea--which banned U.S. beef after the first discovery of mad cow disease in 2003. Still, if there's one industry in a position to convince consumers that its products are safe, it's the beef industry.<p>
"The USDA and meat industry have been really adept at minimizing coverage of issues related to mad cow disease," says Farsetta. "The Public Relations Society of America gives out industry awards for best public relations campaigns, and the National Cattlemen's Beef Association has won several awards for its efforts to spin coverage of mad cow disease."<p>


