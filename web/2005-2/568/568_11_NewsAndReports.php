<font face="Times New Roman, Times, serif" size="4"><b>Stop Maryland's racist death penalty</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Rob Savidge</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BALTIMORE--People are fighting to save the life of Wesley Baker, who was set to be executed the week of December 5. <p>
On November 30, in solidarity with the national effort to save death row prisoner Stan Tookie Williams, more than 40 people gathered to attend a "Live From Death Row" event, where Maryland death row prisoner Vernon Evans was scheduled to call in. Unfortunately, Evans was not able to call in, but the crowd was moved by hearing his sister, Evangelist Bates, lead a chant of "They say death row, we say hell no."<p>
Two days later, more than 100 people showed up, chanting "They say death, we say no, take Wesley Baker off death row," for a demonstration at the Baltimore Supermax prison that houses Maryland's death row prisoners. Listening to the prisoners join in their chants against the death penalty served to remind people how urgent the fight is and to re-affirm the power of protest. <p>
The event--which brought together family and friends of Wesley Baker, students from Goucher college, activists from the Baltimore Coalition against the Death Penalty, Amnesty International, the Campaign to End the Death Penalty, local churches, the Maryland Green Party, the International Socialist Organization and others--ended with a unified call to abolish the death penalty in Maryland. <p>
An organizer from the campaign to save Stan Tookie Williams spoke, linking the local struggle to save Wesley Baker with a national campaign to abolish the racist and anti-poor death penalty.<p>

<font face="Times New Roman, Times, serif" size="4"><b>No to war and occupation</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ABOUT 300 people at the <b>University of Texas at Austin </b>gathered December 1 to protest the U.S. occupation of Iraq and the Israeli occupation of Palestine in one of the largest antiwar rallies since the start of the Iraq War in March 2003.<p>
The event--part rally and part rock concert--was a joint effort by several campus activist groups. Speakers at the event included Mani Mostafi of the Palestine Solidarity Committee; Prof. Robert Jensen; Matt Korn, a member of the Campus Antiwar Movement to End the Occupation; Simon Sedillo of the Austin Indymedia Center; MEChA member Alexis Herrera; Karen Burke of the International Socialist Organization; and Israeli refusenik Stav Adivi.<p>
After the rally, the (International) Noise Conspiracy, a Swedish rock band, played several songs, and singer Dennis Lyxz&eacute;n spoke about the need for people to organize to fight for justice and against oppression everywhere. The rally was a huge success, there's real potential for another collaborative effort by these groups in the future.<p>
-- The student senate at the <b>University of California-Davis </b>passed a resolution December 1 to ban military recruitment on campus. <p>
The military's "don't ask, don't tell" policy is in direct violation of the school's Principles of Community, which bars discrimination based on sexual orientation. More than 10,000 gay and lesbian military personnel have been fired since the policy was implemented in 1993.<p>
Eight campus organizations co-sponsored the resolution including Davis Students Against War (DSAW), the National Organization for Women, the International Socialist Organization, Students for Justice in Palestine and the Davis College Green Party. More than 60 people packed the senate during two-and-a-half hours of debate, which culminated in an 8-4 vote.<p>
"The resolution criticizes the bigoted aspects of the military," said Ellen Mitchell, co-author of the resolution and member of DSAW, which is a chapter of the national Campus Antiwar Network. "We're asking the student body to take a stand against institutionalized discrimination and not apply double standards."<p>
The struggle to get military recruiters off campus is far from over. Since the resolution is nonbinding, activists will have to step up the pressure on UC-Davis Chancellor Larry Vanderhoff. <p>
<i>Call Chancellor Larry Vanderhoff at 530-752-2065 or e-mail <a href="mailto:lnvanderhoef@ucdavis.edu" target="_blank">lnvanderhoef@ucdavis.edu</a>, and tell him to implement the resolution</i>.<p>
-- In <b>Rochester, N.Y., </b>more than 70 people braved frigid weather on December 1 to protest the war in Iraq and demand more spending on social services.<p>
The event was timed to coincide with the beginning of the Montgomery Bus Boycott 50 years ago and brought together Rochester activists from a broad coalition of more than 20 organizations. The day had a teach-in at the central branch library branch downtown where speakers addressed issues of race, class, poverty and war. <p>
That evening, a crowd gathered at the intersection of State and Main and marched to the Armed Forces Recruiting Station less than a hundred feet away and finally to the Federal Building.<p>
-- In <b>Providence, R.I., </b>since the beginning of the month, activists here doubled the number of Central and Classical High School students who have "opted out" of being contact by military recruiters.<p>
One of the activists is a teacher at Central who signed up some 30 students during his free time in school. On December 2, two students volunteered on the spot to join four activists getting students to fill out forms.<p>
Now, more than 80 have signed up, and students are beginning to circulate the forms at school. Students can send a clear message to military recruiters that they won't kill or die in a war for oil.<p>
<i>Jeremy Atkinson, Joshua Karpoff, Jenny Olson and John Osmand contributed to this report</i>.<p>

