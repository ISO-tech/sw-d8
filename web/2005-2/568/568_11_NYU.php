<font face="Times New Roman, Times, serif" size="4">Grad students' strike forges ahead despite threats</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>NYU plays hardball</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sarah Wolf, GSOC</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 9, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--New York University (NYU) President John Sexton greeted the fourth week of a strike by teaching assistants demanding union recognition by threatening that we will lose our entire spring stipends and our eligibility to teach.<p>
Even TAs who return to work would be bound to report all absences to the dean, or risk losing stipends and appointments for two consecutive semesters. These conditions are particularly threatening to international students, who can't legally work outside the university.<p>
Sexton's threats were set to go into effect December 5, but he postponed the deadline to December 7 at the last minute. This was supposedly due to a "compromise proposal" by a mysterious "Graduate Affairs Committee," but clearly was the result of the university's shock at the realization that the vast majority of graduate students would not be returning to work despite the threat.<p>
Sexton's strong-arm tactics followed a big show of solidarity in which strikers, undergraduate supporters and others held a noisy protest in the NYU Library, where Sexton's office is located. <p>
His threats are unprecedented in the history of graduate student strikes and show how far NYU is prepared to go to use an anti-union National Labor Relations Board ruling that disallows bargaining rights for graduate employees at private universities.<p>
The strike by the Graduate Student Organizing Committee/UAW Local 2110 began November 9 in order to force NYU to recognize the union and bargain. If Sexton breaks our union, Columbia, Yale and other private schools with graduate employee unions will follow suit.<p>
Despite these harsh measures, we have resolved to stay on strike until the university negotiates with GSOC.<p>
The process of standing up collectively to intimidation, however, has taught us several lessons about the kind of organization we need as workers and as students. GSOC has reached out to part-time college instructors by asking them to refuse to take adjunct work at NYU for the spring semester--that is, to refuse to act as scab labor. <p>
What's more, more union members are debating the union leadership's idea that we can win this strike simply by "lasting one day longer than the administration." In fact, many members who had never been to an organizing meeting before suggested last week that we put more energy into tactics such as mounting picket lines to stop deliveries and pickups by union drivers. Others have proposed sit-ins at university buildings.<p>
More confrontational and disruptive picket tactics, along with more local, national and international solidarity--and more debate about all of this within our union--can increase the pressure on the university.<p>
