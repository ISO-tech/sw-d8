<font face="Times New Roman, Times, serif" size="5"><b>The FBI's police-state program</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 9, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">SINCE THE passage of the USA PATRIOT Act following September 11 attacks, the Bush administration has dismissed the concerns of civil liberties advocates that the law gives the government unprecedented to snoop into everything from library records to phone conversations and more.<p>
According to the administration, the law is necessary to detect "terrorists" operating on U.S. soil. But last month, the <i>Washington Post </i>revealed that the FBI has been routinely seeking private information under the PATRIOT Act with little or no justification.<p>
According to the <i>Post</i>, the FBI now issues more than 30,000 "national security letters" (NSLs) each year--up from a few hundred a year before the PATRIOT Act.<p>
The letters were created in the 1970s to allow the FBI to get around consumer privacy protections in instances of so-called espionage and terrorism cases. But under the PATRIOT Act, the Bush administration has used NSLs on a massive scale for routine searches into the records of many people who aren't accused of being terrorists or spies.<p>
Telephones records, correspondence, financial records, even records of what people read online at a public library--all are fair game for the Feds under the PATRIOT Act. The FBI simply has to claim that the records are "sought for" or "relevant to" an investigation "to protect against international terrorism or clandestine intelligence activities."<p>
And the invasion of privacy doesn't stop there. In the past, information collected as part of these investigations was supposed to be kept confidential--and files on innocent people and companies had to be destroyed once an inquiry was finished. But in 2003, Bush signed an executive order allowing "state, local and tribal" governments, as well as unspecified "appropriate private-sector entities," access to the information collected by the Feds--which is now deposited into government data banks.<p>
Joseph Billy Jr., the FBI's deputy assistant director for counterterrorism, recently told the <i>Post </i>that innocent Americans "should take comfort at least knowing that it is done under a great deal of investigative care, oversight, within the parameters of the law." But that's simply not true when it comes to the PATRIOT Act, according to recent reports.<p>
In late October, records turned over as part of a Freedom of Information Act lawsuit filed by the Electronic Privacy Information Center (EPIC) revealed that the FBI had conducted secret surveillance on some U.S. residents for as long as 18 months at a time without proper paperwork or oversight. In other cases, agents obtained e-mails after a warrant expired, improperly seized bank records and, in at least one case, conducted an improper "unconsented physical search."<p>
The FBI says these cases are minor "administrative" errors. But the documents reveal a minimum of 287 potential violations over a span of three years--and the real number is likely much higher.<p>
"We're seeing what might be the tip of the iceberg at the FBI and across the intelligence community," said EPIC's general counsel Dave Sobel. "It indicates that the existing mechanisms do not appear adequate to prevent abuses or to ensure the public that abuses that are identified are treated seriously and remedied."<p>

