<font face="Times New Roman, Times, serif" size="4">Activists kidnapped in Iraq</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"This is a result of U.S. actions"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 9, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">WHILE THE U.S. and British media tried to whip up a hysteria against the "Iraqi insurgency" after the November 26 kidnapping of four members of the Christian Peacemaker Teams (CPT) in Baghdad, support for the human rights activists was gathering among those who oppose the occupation in Iraq.<p>
"The Islamic and National forces in the governorate of Hebron/Palestine have had long experience confronting Israeli crimes and violations with the CPT since 1995, and wish to confirm that the members of this group have had and still have a major role in confronting Israeli crimes and violations, and in the protection of the property and the lives of the Palestinian citizens," read a November 29 statement signed by numerous organizations, including the Islamic Resistance Movement/Hamas, the Democratic Front for the Liberation of Palestine, Fatah and the Popular Front for the Liberation of Palestine.<p>
"More than once they placed themselves in front of the occupation's tanks, and they confronted Israeli occupation bulldozers with their bodies defending Palestinians' homes against destruction...Because of what they were doing, the CPT members were subjected to arrest, beating and pursuit by the Israeli soldiers and settlers in more than one location in Palestine...<p>
"We appeal to our brothers in the resistance and all those with alert consciences in Iraq, with whom we consider ourselves to be in the same trench confronting American aggression and occupation, to instantly and quickly release the four kidnapped persons (two Canadians, one Briton and one American) from CPT, in appreciation for their role in standing beside and supporting our Palestinian people and all the Arab and Islamic peoples."<p>
The CPT came to Iraq in October 2002 to oppose the U.S. invasion and has remained to document the horrors faced by Iraqis under foreign occupation. Their work includes interviewing Iraqis detained in U.S.-run prisons and detention centers, and telling these stories to the world. The CPT was among the first to document the torture at Abu Ghraib prison.<p>
The CPT is the only Western solidarity organization still on the ground in Iraq. It says that it supports the Iraqi people's right to self-determination.<p>
"We are angry because what has happened to our teammates is the result of the actions of the U.S. and U.K. government due to the illegal attack on Iraq and the continuing occupation and oppression of its people," reads a statement posted on the CPT's Web site.<p>
An urgent appeal to save the CPT activists has been endorsed by well-known figures in the antiwar movement, including authors Arundhati Roy, Tariq Ali, Noam Chomsky and Howard Zinn; Rashid Khalidi of the Middle East Institute; Gold Star Families for Peace cofounder Cindy Sheehan: and Craig and Cindy Corrie, the parents of solidarity activist Rachel Corrie, who was killed by an Israeli bulldozer in 2003.<p>
