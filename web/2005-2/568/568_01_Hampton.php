<font face="Times New Roman, Times, serif" size="4">School backs down</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Victory for Hampton antiwar activists</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By David Thurston</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 9, 2005 | Page 1</font><p>

<font face="Times New Roman, Times, serif" size="3">STUDENT ACTIVISTS at Hampton University in southeastern Virginia have won a crucial battle with the school's administration. <p>
Seven students were threatened with expulsion for the "crime" of distributing literature against the war and for social justice as part of a national day of action November 2. But following a contentious hearing organized by administrators last week--in which the students were defended by lawyers, their parents and professors within the restricted hearings, and by dozens of students protesting outside--the university backed down. None of the seven were expelled, though most were ordered to do 20 hours of community service.<p>
"At least 50 students stood outside the hearings, which went on for four to five hours," John Robinson, a leading activist, told <i>Socialist Worker</i>. "Right outside the campus, there were media, people from the community, and students. People honked horns to show support and wore duct tape over their mouths to protest the suppression of free speech."<p>
The victory came as campus activists were preparing for a National Day of Counter-Recruitment on December 6, called by the Campus Antiwar Network, with actions set to take place around the country.<p>
Following the November 2 events at Hampton, a historically Black college, three students were charged at first, Robinson among them. Another four got letters mailed to their homes over the Thanksgiving break notifying them of the disciplinary charges. <p>
The Hampton Seven came back to school to find their hearing scheduled just a few days later--for December 2. But a groundswell of support developed behind the seven--from professors and parents on campus, to students from other nearby schools, to activists across the country.<p>
The administration had planned to wait a few days before announcing its decision on the charges, but instead prepared a verdict in just a few hours. "They're so used to using naked force, said Robinson, "but they had to use restraint, and they didn't know how to handle themselves."<p>
Brandon King, another leading organizer, said that in the days leading up the hearing, teachers talked about the issue in their classes, and larger numbers of students got involved. "One thing that I'm really proud of is the support from other students on campus," he said. Robinson agreed. "The student body now sees this as their fight," he said. <p>
The support that came from the Hampton community and from activists nationally made a huge difference. Students from Amnesty International at Howard University came on the day of the hearing with 912 signatures on petitions of support, circulated in only two days. Hundreds signed an online petition that went up the day before the hearings. "If we had gone in isolated, they would have grilled us," said Robinson.<p>
As King said, "All across the board, faculty, students and workers are clamped down on and not allowed to speak. This showed that we can stand up to them--that the administration is not invincible."<p>
To save face, administrators imposed the 20 hours of community service. As one activist put it, "We got community service for doing community service in the first place."<p>
King emphasized the importance of continuing the fight. "They're not allowing us to organize, which is what students should be doing," he said. "A real victory would be if we had an organization on campus that was allowed to do that. Our group is anti-racist and anti-sexist, and our leadership shows that. All of these struggles, on and off campus, working with unions, with anti-military recruitment, and even with people in the military who are against the war--in order to win, we've got to see every struggle as interconnected."<p>
Students at Hampton will be deciding on how to continue to press their demands for free speech on campus and the rights of activists to organize. Their struggle is a decisive step toward raising the profile of the antiwar movement and the left at historically Black colleges and universities. The national support and solidarity that helped win this round will be crucial for the battles to come.<p>

