<font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>How mad is King George?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 9, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">AGAINST THE advice of the Washington punditocracy that he needed to change course in Iraq, or at least give the appearance of changing course, George W. Bush returned to his standard talking points in his November 30 speech at the Naval Academy.<p>
"Despite the gathering storm of opposition to the administration's approach to the war in Iraq, the speech was long on tired clich&eacute;s and bereft of new ideas, calling to mind the words of Emerson: 'A foolish consistency is the hobgoblin of little minds,'" wrote Ray McGovern of Veteran Intelligence Professionals for Sanity.<p>
The image of an out-of-touch chief executive lends credence to the stories seeping out of the White House suggesting that Bush is either losing his mind or hitting the bottle again. For example, a Seymour Hersh <i>New Yorker</i> expos&eacute; on military plans to rely more heavily on air power in Iraq, presented a picture of Bush in "the gray world of religious idealism where he wants to be anyway" while Dick Cheney and Karl Rove run the show.<p>
These stories reinforce the liberal conceit that Bush--and by extension, his supporters in "Red State America"--is just a dumb rube who unwittingly carries out the wishes of a neconservative cabal. Nevertheless, a good amount of skepticism is in order.<p>
First, for an administration that is known to manufacture news stories, anything that purports to be an "inside the White House" revelation should come with a warning sticker. <i>Newsweek's</i> 2004 "inside the campaign" coverage showed that the media's reporting that the White House was trying to contain outrage about its use of 9/11 images in campaign ads belied what was really going on: Bush's advisers were ecstatic to bring the campaign focus back to terrorism and 9/11.<p>
Second, who benefits from these stories about Bush's incompetence? They followed behind other stories claiming that Bush felt betrayed by Rove's role in the Valerie Plame scandal and that he felt alienated from Cheney, who had assured him that everything would work out in Iraq.<p>
So if the earlier revelations were intended to insulate Bush from Cheney and Rove, one can speculate on the origin of the "Bush is out to lunch" stories. Perhaps Cheney's supporters in the White House bureaucracy convinced of their boss's indispensability.<p>
Another is the wing of the Republican Party--led by Bush Sr.'s National Security Adviser Brent Scowcroft--that wants to discredit neoconservative "idealism" in order to devise a "realist" strategy to succeed in Iraq and elsewhere.<p>
Third, even if we assume that all of these stories about Bush are true, it doesn't really matter. Had the Iraq adventure gone the way they expected it to, we'd still be reading stories about Bush like Bob Woodward's fawning portrait in <i>Bush at War </i>of the take-charge commander with a subtle understanding of international politics.<p>
Instead, the Iraq debacle has put a significant challenge to the U.S. establishment that it hasn't fully figured out how to tackle. This isn't just a challenge to a White House "cabal," but to a bipartisan establishment that agrees that Iraq can't be lost.<p>
If Bush is an out-of-touch nutcase, what does that make Sen. Joe Lieberman, the Democrats' 2000 vice presidential nominee, whose recent <i>Wall Street Journal</i> op-ed on progress in Iraq read like a White House press release? Or what about Democratic Sens. John Kerry, Joe Biden or Hillary Clinton, whose plans for Iraq, rhetorical differences aside, are not that much different than Bush's?<p>
Despite Bush's posturing about "strategies for victory" and "staying the course," it appears that the Pentagon, Congress and the White House are converging on a policy of "drawdown," or Iraqization, to take shape over the next year. The aim is to "redeploy" U.S. troops from Iraq, "stand up" the puppet government, and present it as what the administration planned to do all along.<p>
And if the politicos at the White House aren't dodging indictments next year, they will be devising a strategy for Bush to take credit for this.<p>

