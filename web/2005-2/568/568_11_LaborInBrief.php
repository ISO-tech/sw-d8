<p><a name="Capital"></a><font face="Times New Roman, Times, serif" size="4"><b>Capital Metro</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jason Netek</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AUSTIN, Texas--In recent weeks, the conflict between Capital Metro and the drivers, mechanics and other workers of Amalgamated Transit Union Locals 1091 and 1549 has intensified.<p>
Last month, Local 1091 voted 289-24 to authorize a strike after rejecting its employer StarTran's "last, best and final offer." The union has already staged a one-day strike as well as several pickets to demonstrate that it's not backing down. <p>
The union opposes management's proposed two-tier wage system as well as several other union-busting tactics and health care cuts. By authorizing a strike, the union has been able to get back to the bargaining table, even though it supposedly had already received Capital Metro's "final offer." <p>
Local 1091 President Jay Wyatt declared that the union will continue to bargain, and he called StarTran's bluff about forcing a contract. "If they do, we're going out on strike," said Wyatt. "It's as simple as that." <p>
Two weeks ago, Local 1549 held an assembly to discuss striking over the unfair labor practices of its employer, FirstTransit, which has yet to make good on the contract already set in place by former employer ATC/Vancom. As a result, the drivers are working under an expired contract. <p>
A vote to authorize a strike has yet to take place, but members recently elected a new president--Glenn Gavin, who's a driver, a former recording secretary and a union militant--showing that pro-strike sentiment has a hearing in the union. Student activists were recently invited to speak at the assembly and have pledged to assist the union in building solidarity and strike support.<p>

