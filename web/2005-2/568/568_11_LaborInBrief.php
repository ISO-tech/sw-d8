<font face="Times New Roman, Times, serif" size="4"><b>Metropolitan Transit Authority</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Peter Lamphere</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--The men and women who make this city move were angered last week as the Metropolitan Transit Authority (MTA) ratcheted up its demands in the negotiations for their new contract.<p>
The MTA has proposed that the separate jobs of conductor and train operator be merged into a single position and that token booth clerks take on cleaning responsibilities inside train stations--in addition to refusing to budge on wages. The 34,000-member Transport Workers Union (TWU) Local 100 has said it will go on strike if a new settlement is not reached by midnight on December 15.<p>
Since the MTA is a state agency, Mayor Michael Bloomberg claims to be neutral in the talks. But in 1999 and 2002, the mayor's office obtained injunctions against a transit strike, which are illegal under New York State's draconian Taylor Laws banning public-sector strikes.<p>
This time, the union has called the city's bluff, asking a judge to place an injunction on any such maneuver by the city.<p>
However, the results of the transit workers' battle will be determined by the actions of the rank and file. Already, some train operators and yard workers have been engaging in a work-to-rule campaign that can slow the system down.<p>
TWU Local 100 has a long history of rank-and-file activism--and union oppositionists even managed to win control of the union in 2000 when the New Directions slate elected current president Roger Toussaint. Yet he has turned out to be a top-down leader who has yet to mobilize the rank and file in the contract campaign.<p>
A showdown is looming. Bloomberg has already won starkly concessionary contracts from firefighters, sanitation workers and teachers. Toussaint has pledged to win a "no giveback" contract. But that will only happen if the rank-and-file bus drivers, train operators and other transit workers are mobilized to make the strike threat a credible one.<p>

