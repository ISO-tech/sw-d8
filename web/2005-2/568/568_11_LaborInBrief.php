<p><a name="NWU"></a><font face="Times New Roman, Times, serif" size="4"><b>National Writers Union</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar, NWU</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THE NATIONAL Writers Union is holding a special election to fill vacancies opened by resignations--the first since reformer Jerry Colby was elected president in late 2003. <p>
Reform efforts have been hampered over the last year by problems inherited from the previous leadership of Jonathan Tasini and disunity in the current leadership.<p>
The slate of Jack Rasmus for First Vice President, Ken Wachsberger for Second Vice President, and Dan McCrory for Recording Secretary offers the best candidates to continue the organizing and rebuilding efforts initiated by Colby. Larry Goldbetter, while not formally part of this slate, is the best choice for Trustee.<p>
On the other hand, the Conroy-Eisenberg-Selby slate would obstruct the consolidation of reform efforts.<p>
They are politically questionable as well. Seth Eisenberg, the At Large Chapter Co-Chair and candidate for Second Vice President, worked for American Israel Public Affairs Committee (AIPAC) and was National Executive Director of the right-wing Likud USA. Eisenberg requested that the cash-strapped union fly an at-large delegate from Israel to the recent National Delegates Assembly at the cost of $2,000.<p>
The election of Rasmus, Wachsberger, McCrory and Goldbetter would provide the National Writers Union with a united leadership to carry through President Colby's reforms, reach out to youth and focus on organizing book writers.<p>

