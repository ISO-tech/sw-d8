<p><a name="Vt"></a><font face="Times New Roman, Times, serif" size="4"><b>University of Vermont</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Tristan Brosnan</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BURLINGTON, Vt.--More than 200 hundred people, including students, faculty, staff and construction workers from the University of Vermont (UVM), met outside the Board of Trustees lunch to rally in support of good jobs. <p>
The faculty union at UVM has been in the negotiation process for 11 months. Staff members, who have undertaken a union-organizing drive, have recently been told that they can't talk about the union drive at work. <p>
During the summer, UVM spent $20,000 on an anti-union Web site to try to persuade staff members not to organize. The scheme backfired, however, leaving students, construction workers, faculty and staff fed up. <p>
On the morning of the rally, the Board of Trustees received two petitions--one from students and one from staff--with more than 1,000 signatures. "It's all of our futures," said Jon Mandel, a freshmen at the University of Vermont, at the rally. "We need to stop putting money toward war and start giving it to educators and workers."<p>
Students and staff marched into the building where the Board of Trustees was meeting. After a few minutes, the board decided to leave, but only after being followed by more than 100 people, chanting "Money for the classroom, not the boardroom."<p>
The event was a major success. "It's happening everywhere--teachers at Colchester went on strike, and transportation workers in Philadelphia recently went on strike," said Jenn Shainess, a senior. "It's a big deal."<p>

