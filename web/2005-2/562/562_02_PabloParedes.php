<font face="Times New Roman, Times, serif" size="4">Pablo Paredes' fight goes on</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Navy discharges a war resister</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 21, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">PABLO PAREDES, the Navy sailor who last year refused to board his ship, the <i>USS Bonhomme Richard, </i>when it left port for Iraq, was discharged from the Navy on October 6. The Navy failed to get the bad-conduct or "conditions other than honorable" discharge it wanted. Paredes was discharged administratively "under honorable conditions."<p>
In December of last year, Paredes, a third-class petty officer, refused to board the ship going to Iraq, saying at the time, "I can't sleep at night knowing that's what I do for a living." "I'd rather do military prison time than six months of dirty work for a war that I and many others do not support," he told the <i>San Diego Union Tribune.<p>
</i>Pablo turned himself in to military authorities, and in January, he formally applied for conscientious objector status. He was court-martialed for unauthorized absence and missing movement, which carries a maximum punishment of one year in confinement and a bad-conduct discharge.<p>
At his special court-martial proceeding on May 11, Paredes pled "not guilty" on the grounds that the war in Iraq was illegal under international law, and that he had a duty not to participate in it.<p>
Supporters organized a grassroots campaign around the country to support Pablo, including mock trials featuring testimony from antiwar vets, their families and antiwar activists, which put Bush and the war--not a war resister like Paredes--on trial.<p>
The unauthorized movement charge was dropped, and Paredes was found guilty of missing movement--with the punishment being two months restriction, three months hard labor without confinement, and reduction in rank to the lowest pay grade.<p>
Now, Pablo is a civilian. "The discharge marks the end of Paredes' military career, but not the end of his legal battle with the Service, and certainly not the end of opposition to the war within the ranks of the military," read a statement released by Citizens for Pablo.<p>
The fight continues for Paredes, whose lawyers are challenging in federal court the Navy's rejection of his request for conscientious objector status. "As the war goes on, it is becoming increasingly clear for both those in the Service and out that, as Cindy [Sheehan] pointed out, there is no noble cause," said Paredes in a statement last week. "Honor and conscience demand that we all speak out. I did so while I was in the Navy, following the examples of Camilo Mej&iacute;a and others. Others will follow in our footsteps until this madness ends."<p>

