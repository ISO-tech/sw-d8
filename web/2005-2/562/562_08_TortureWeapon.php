<font face="Times New Roman, Times, serif" size="5"><b>The Pentagon's torture weapon</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 21, 2005 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">JUST WHEN it seems that things can't get any worse, we learn that, despite setbacks, the Pentagon is still pursuing the development of its new microwave gun. <p>
This will be the ultimate torture weapon. Its purpose is to cause excruciating pain, but leave no evidence of wounds on the victim. Imagine this weapon at Abu Ghraib or Guant&aacute;namo. Imagine this weapon at your local police precinct. <p>
The Department of Defense has named this weapon system "Project Sheriff." It is the ultimate crowd-control weapon--a ray gun that could literally make your blood boil. It's based on the same technology as a microwave oven. The human body is comprised mostly of water...think of the sensation of boiling blood. The purpose is to cause an unbearable level of pain, so that the victim will submit to the will of the "Sheriff."<p>
The gun produces a 95-gigahertz microwave beam that is designed to penetrate the skin by only 1/64th of an inch. Should the experts be trusted to achieve zero defects with a technology that requires so precise a tolerance?<p>
This project brings to mind some questions. Is this system being designed primarily for domestic use against U.S. citizens? Will it be used for "crowd control" at sites of labor disputes and strikes? Will it be coming soon to a war protest near you? Will it be used at the borders to prevent immigration? Does international law prohibit the use of this weapon on the battlefield? Will the government hide behind sovereign immunity when a citizen is injured or killed by this weapon? How will this weapon effect children? Will the NRA lobby for access to this weapon? How will it affect the performance of an implanted medical device, such as a pacemaker?<p>
The good news is that the Pentagon is having some major design problems. The designers have failed to realize that a person is not a potato. Microwaving a human to the exact degree of doneness is proving to be problematic. <p>
Is there anyone out there who wants to volunteer as a subject for the field tests? What they need is a test subject, with a pacemaker, contact lenses, a lot of amalgam dental fillings, and maybe a few metal surgical staples from an old appendectomy. Will they pay a bonus if the subject is pregnant? When the experiment is completed, if the subject is incapacitated, but still alive with no visible wounds, the field test is a success.<p>
Some $51 million has already been spent on this weapon system. This gun has killed before the trigger was even pulled. In the U.S., 18,000 die each year because of the lack of health care. If that $51 million had been used to provide health care to our fellow citizens, many lives would have been saved. <p>
The real enemies of the American people are those whose priorities are so warped that they allocate money for ray guns while ignoring the humanitarian needs of the populace. The Pentagon, with the help of Congress, has become like a gluttonous dinner guest, demanding just one more pork chop. <p>
The military wants just one more weapon system, even though it already has enough firepower and nuclear power to kill every living thing on the planet. The voracious appetite of the Department of Offense will continue until the people rise up and say, "Enough. Not one more dollar."<p>
Raytheon, with headquarters in Waltham, Mass., is listed as the prime contractor on this project. It is the ultimate irony that Raytheon claims on its Web site to have received an award for ethics. <p>
The design and production of redundant weapon systems is pushed by the lobbying efforts of the arms manufacturers, who have been doing a land-office business. Somehow, all of this seems to be okay with the employees of Raytheon and the other arms dealers. The argument that "we need the jobs," is an old one that has been used to justify the development of the most horrific weapons. <p>
The blood of victims of U.S. aggression falls on all of us--taxpayers, engineers, designers, all of us who are involved directly or indirectly in the production of weapons. The time has come to say, "Enough, not one more new weapon system. Not one more dollar for offense. Not one more death."<br>
<b>Rosemarie Jackowski, </b>from the Internet<p>

