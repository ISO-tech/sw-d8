<font face="Times New Roman, Times, serif" size="5"><b>Millions More draws over 100,000 to D.C.</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By David Thurston</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 21, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">WELL OVER 100,000 people streamed onto the National Mall in Washington, D.C., for the Millions More Movement rally organized by the Nation of Islam (NOI) and a coalition of Black organizations.<p>
The crowd came from far and wide. Many seemed to have organized themselves to get there, determined in particular to express their anger at the government's callous approach to the victims of Hurricane Katrina, and to stand up against the systematic institutionalized racism of U.S. society.<p>
The Millions More event was held on the 10th anniversary of the Million Man March, but both the call for this year's rally and speeches at the event itself were more left wing compared to the 1995 march, which focused on how Black men themselves were responsible for problems caused by racism and poverty.<p>
This time, NOI leader Louis Farrakhan repeated some of the same rhetoric about "personal responsibility," but his speech also denounced the U.S. war on Iraq and emphasized the huge gap between rich and poor in the U.S. <p>
Damu Smith from Black Voices for Peace called on the crowd to raise their hands in support of bringing the troops home now--and again, to see the billions spent on war go to relief in the wake of Hurricane Katrina. <p>
Elaine Johnson, also of Black Voices for Peace and the mother of a soldier killed in Iraq, echoed the antiwar message. "I need more Black mothers affected by this war to speak up and tell George Bush to bring the troops home now," Johnson said.<p>
Tony Muhammad, a leading NOI figure in Los Angeles, delivered a blistering indictment of police brutality, pointing out that "Black and brown cops" can be just as brutal and racist as whites.<p>
There were also several speakers from the labor movement. Chris Silvera, president of the Teamsters Black Caucus, called the march a "defining moment" in the effort to unite labor, social justice and inter-faith organizations together to fight for common goals. Clarence Thomas of the International Longshore Workers Union talked about the struggles facing workers on the Gulf Coast. "We need to build a movement," he told the crowd. "We <i>will</i> have a prevailing wage...There <i>will</i> be the right of return" for Gulf residents.<p>
The final speakers focused on Stan Tookie Williams--the California death row prisoner who faces a December 13 execution date. Barbara Becnel, Williams' advocate, spoke from the front about the urgency of the struggle to stop this execution. Meanwhile, members of the Campaign to End the Death Penalty circulated petitions supporting Williams. Hundreds signed them, and asked what they could do to get involved.<p>
The event was marred by the refusal of organizers to grant a role to Black gay organizations. During organizing for the event, gay rights organizations protested a vicious anti-gay sermon by a march leader, Rev. Willie Wilson, and won assurances from organizers that they would be able to speak.<p>
But on the morning of the rally, Keith Boykin of the National Black Justice Coalition was barred by Wilson, though there was one gay speaker from another organization. Days earlier, Wilson delivered an unhinged tirade about Black girls using sleeping pills to turn other Blacks into lesbians.<p>
This bigotry needs to be confronted. Discrimination against gays has no place in a movement genuinely committed to fighting racism. Nonetheless, the turnout was a clear expression of the potential for rebuilding the fight against racism.<p>
<i>Nihar Bhatt and Ben Dalbey contributed to this report</i>.<p>

