<font face="Times New Roman, Times, serif" size="5"><b>Antiracists confront the Minutemen in Chicago</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Dustin Simons</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 21, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">ARLINGTON HEIGHTS, Ill.--More than 300 people protested the anti-immigrant vigilante group called the Minutemen at the group's "boot camp" October 15. The aim of the meeting--which took place in the same suburb where the Ku Klux Klan tried to organize a demonstration a few years ago--was to recruit and train.<p>
The counter-demonstrators, made up of mostly Latinos, brought personal experience to the rally. They let out a roaring chorus of Spanish and English chants against the racism of those gathered inside.<p>
The crowd stayed strong despite intimidation tactics from Arlington Heights police. An open paddy wagon remained parked in front of the largest group of protesters, and police in full riot gear waited across the street. <p>
At one point, police rushed at the rally, targeting those who had locked arms earlier, successfully blocking the entrance. Starting at 7:30 a.m., activists had locked arms for hours and prevented the one entrance of the building from begin used by "trainees."<p>
People were thrown to the ground and arrested--including a Muslim woman, whose hijab was ripped off by a female police officer.<p>
One demonstrator with a sign that read, "The Minutemen are the illegals," was with a group from nearby Harper College, which participated in the early morning action. "We are in a country founded on immigration," she explained when asked why she was there.<p>
Juan Torres, a member of the antiwar group Gold Star Families for Peace, which is made up of family members of those who died in Iraq and Afghanistan, also joined the protest. A participant in actions at Camp Casey in Crawford, Texas, Torres carried a photo of his son, who died while serving in Afghanistan. "Whether it's the Bush regime, the Minutemen or the Ku Klux Klan, the oppression is the same," he said.<p>
Whatever form it takes and wherever it is found, racism and bigotry have to be met with our full opposition.<p>

