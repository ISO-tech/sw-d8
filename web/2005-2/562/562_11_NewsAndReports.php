<font face="Times New Roman, Times, serif" size="5"><b>News and reports</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">October 21, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Military off our campus</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jocelyn Blake</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN DIEGO--Activists from the Campus Antiwar Network (CAN) and the International Socialist Organization turned out last week to demand that the military get off of our campuses.<p>
The protest came as military recruiters "occupied" San Diego State University (SDSU) for three days last week, putting on an extravagant display with Humvees, war simulation games and a rock-climbing wall. Recruiters even rappelled off of buildings and parachuted out of aircrafts as part of their attempt to project an image of the military as a hub of adventure and excitement.<p>
But protesters turned out to expose the reality of life in the military: war, discrimination, sexual harassment and unfulfilled promises of opportunity.  <p>
Ten activists surrounded the recruiters during one of their rappelling stunts and demanded that the military leave our campus. Within minutes, campus security showed up and tried to shut the protest down. When activists refused to leave, security became even more aggressive--despite the fact that it is against California State University policy to confine free speech on campus to a designated "free-speech zone."<p>
As the confrontation escalated, a group of bystanders grew, and several joined the protest against the recruiters and defended the right to free speech. Even the dean and assistant dean of the university left their offices and came to the defense of protesters, and campus police were eventually forced to leave in frustration.<p>
Not a single person stopped to talk to a military recruiter, while CAN was able to connect with dozens more students who want to make the antiwar majority known at SDSU.<p>

