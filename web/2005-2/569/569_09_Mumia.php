<font face="Times New Roman, Times, serif" size="5"><b>A step forward for Mumia</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 16, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">PENNSYLVANIA DEATH-row prisoner Mumia Abu-Jamal will get to plead his case in a federal appeals court early next year. In a surprise decision earlier this month, the Third U.S. Circuit Court of Appeals agreed to hear arguments on three important points where his Constitutional rights were violated.<p>
The three claims are among many that Mumia's legal team has pursued. One concerns the prosecutors' use of peremptory challenges to keep qualified African Americans off the jury. Another deals with the bias displayed by the late Judge Albert Sabo, who presided over Mumia's trial and post-conviction appeals. The third concerns the prosecutor's statement during closing arguments that Mumia would get "appeal after appeal"--implying that jurors needn't feel too responsible if they voted to convict.<p>
Any one of these claims, if upheld by the three-judge panel, could lead to a new trial.<p>
Mumia is a journalist and former Black Panther who was convicted and sent to death row for the 1981 shooting of a Philadelphia police officer. After more than two decades on death row, he remains one of the most powerful voices against the death penalty and the U.S. injustice system as a whole.<p>
Much of this latest appeal revolves around the factor that has plagued Mumia's case from the beginning--racism.<p>
Mumia's lawyers have shown that the Philadelphia district attorney's office rejected roughly three out of four qualified Black jurors in murder trials--three times the rate for white jurors. As Dave Lindorff, author of a recent book exposing the flaws in Mumia's case, wrote on the CounterPunch Web site: "[T]he judges would be hard-pressed to find it fair in a city 44 percent Black that the jury selection process in Abu-Jamal's trial resulted in his having just two Black jurors ruling on his guilt and sentence."<p>
Presiding over the racist circus that passed for Mumia's trial was Albert Sabo. "Despite having been retired for over 10 years and dead for four," the New York City Free Mumia Coalition has noted, "Sabo still holds the dubious distinction of having sentenced more people to death than any other judge in modern times." Of the 31 people Sabo sent to death row during his career, only two were white.<p>
In Mumia's case, Sabo openly vowed outside the courtroom during the trial that he was going to help prosecutors "fry the nigger," according to an affidavit from a longtime court reporter.<p>
Mumia's appeal is on a "fast track"--the first deadline for the defense to file its legal brief is in mid-January. Since 2001, when his death sentence was overturned, Mumia's case has been at a seeming standstill. The new year could change that in a hurry.<p>

