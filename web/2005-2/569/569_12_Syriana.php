<font face="Times New Roman, Times, serif" size="5"><b>Politics of oil</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Geoff Bailey</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 16, 2005 | Page 12</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Syriana, </b></i>directed by Stephen Gaghan, starring George Clooney, Matt Damon and Jeffrey Wright.</font><p>

<font face="Times New Roman, Times, serif" size="3">THE MOST amazing thing about <i>Syriana</i> is that it was even made.<p>
Stephen Gaghan's follow-up to his Oscar-winning screenplay for <i>Traffic</i>,<i> </i>is a labyrinthine political thriller about the violence and corruption that surrounds the politics of oil.<p>
A few years ago, Hollywood executives would have run away from this story faster than George Bush from his National Guard training. And the fact that <i>Syriana</i> is being billed as a major holiday movie shows that, even among Hollywood moguls, there's recognition that something's changing: There's a broad audience for political films that challenge the priorities of the Bush administration.<p>
<i>Syriana </i>revolves around the merger between Connex Oil and Killen Oil, in what would produce the world's largest oil company. <p>
Around this, Gaghan weaves together a half dozen stories: Bob Barnes, a world-weary CIA assassin; Bryan Woodman, an ambitious young energy analyst; Bennett Holiday; an up-and-coming lawyer charged with investigating the merger; Jimmy Pope, the CEO of Killen Oil; and Nasir Al-Subbai, a Gulf State prince with eyes on his father's throne and plans to modernize his country, challenging the U.S.'s grip on the country.<p>
And these are just the main story lines. A review can hardly do justice to the way various characters intersect and overlap. Part of the fun of the film is how these separate stories come together to form a complete picture.<p>
Within this structure, Gaghan also adds a biting critique of how America's dependence on oil leads to political intrigue and corruption. When Bennett confronts a wealthy political consultant about his role in the Connex/Killen deal, he responds: "We have laws against [corruption] precisely so we can get away with it. Corruption is our protection. Corruption is what keeps us safe and warm...Corruption...is how we win."<p>
Politicians are bought and sold, laws are broken with impunity and, when oil profits are at stake, the U.S. government is more than willing to abandon its stated aim of creating democracy in the Middle East. And the structure of Gaghan's film allows for individual stories of redemption and the punishment of a few scapegoats, but the system continues.<p>
No hero will come to the rescue to save the day. A larger critique is needed, as is action beyond the film world.<p>
In fact, the <i>Syriana</i> Web site is linked to a site created by the production company Participant Films (www.participate.net/oilchange) that urges people to become active in reducing America's dependence on oil. Not surprisingly, the politics of the site are fairly conservative, mainly aimed at petitioning Congress to increase spending on developing alternative energy sources.<p>
After watching the film, though, it would seem hard to believe that these actions will have much effect. But the fact that the film raises serious questions about U.S. foreign policy and encourages its audience to get involved in changing those policies is a unique thing in Hollywood. Not to mention that the film itself is extremely entertaining.<p>
The only criticisms of the film are that there are so many different storylines that most of the characters come across as thin stereotypes. This is especially true of the portrayal of the Arabs in the film.<p>
Second, the war in Iraq is conspicuously absent from the film. While <i>Syriana </i>exposes the dirty secrets of the U.S. government's attempts to protect a free flow of oil from the Middle East, it ignores the obvious.<p>
Today, many of the most heinous acts committed by the U.S. government are not being committed by shadowy figures behind closed doors, but in full view. The failure of the film to tackle that subject weakens its impact and its aim at being relevant.<p>
These criticisms aside, though, <i>Syriana </i>is the most damning critique of U.S. policy that Hollywood has ever produced.<p>

