<font face="Times New Roman, Times, serif" size="5"><b>Viewpoint of a suicide bomber</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Hadas Thier</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 16, 2005 | Page 12</font><p>

<b><i><font face="Arial, Helvetica, sans-serif" size="2">Paradise Now, </b></i>directed by Hany Abu-Assad, starring Kais Nashef and Ali Suliman.</font><p>

<font face="Times New Roman, Times, serif" size="3">THE MAINSTREAM media pores over in gory detail every death that has ever resulted from a suicide bombing. We see the wailing family members and dismembered limbs in one simple and bloody, final consequence. <p>
Every death is bemoaned but one. The suicide bombers themselves are always omitted from the stories. Even if Palestinians as a whole are demonized, the individual who carried out the act is utterly invisible. <p>
What if the opposite were true? What if we could see the story through their eyes? Understand the context and tortured decisions leading up to this final sacrifice? Hany Abu-Assad's <i>Paradise Now</i> does just that.<p>
Said and Khaled are two lifelong friends living in Nablus (part of the Israeli-occupied West Bank) who are recruited to carry out a suicide mission in Tel-Aviv. In following their day, we see grinding poverty and the ever-present humiliation, boredom and degradation of life under occupation.<p>
The occupation seeps into every mundane detail, from water filters to military checkpoints, to occasional shocking, but not unexpected, rocket blasts. It is this daily context that paints a clear picture of hopelessness and rage.<p>
Just as brilliantly, <i>Paradise Now</i> portrays the struggle among Palestinians (and within individuals) in regards to suicide bombing through discussions and arguments among the characters. Never posed morally, the question is tactical: Will sacrificing your life make a difference to the occupation?<p>
For Said, it's not the individual act of the suicide bombing, but what it represents in the continuation of the struggle that matters. "Life here is like life imprisonment," he explains, "...day after day of humiliation and weakness and the world watches cowardly, indifferently."<p>
But Israel has managed to convince the world that they are victims. "How can the occupier be the victim?" he asks. "They leave me no choice but to be a victim and murderer as well."<p>
With the exception of the leaders of the nameless resistance organization, who are depicted in too much of a two-dimensional way as somewhat cynical and manipulative, the characters of <i>Paradise Now</i> have a depth that gives justice to Palestinian life and resistance. The warmth, compassion and humanity between the characters defy the soul-crushing weight of the occupation.<p>

