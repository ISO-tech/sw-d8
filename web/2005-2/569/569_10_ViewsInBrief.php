<font face="Times New Roman, Times, serif" size="4"><b>DNC is part of the problem</b></font><p>

<font face="Times New Roman, Times, serif" size="3">HOW IN God's name did we end up with this gutless Democratic National Committee (DNC)? Isn't it time to realize that DNC Democrats are part of the problem, not the solution? <p>
Granted, the fascists are shooting themselves in the foot pretty good these days with their treasonous lies, but you can't win a sports competition if you never get any points on the board. <p>
So here we are, stuck with Tweedledee and Tweedledumber: the DNC and the RNC. Good ol' boys all! Clearly, the big enemy here is "machine politics"--Republican or Democrat. <p>
Let's face it, these "professionals" don't give a rat's derriere about us; They're too busy feeding at their designer hog troughs and trying to get on late-night television. Being on the DNC is their thing. They get their hits of status and bucks, seasoned with a little extramarital fun and games, so who bloody cares why we put them there in the first place?<p>
Well, since those losers have obviously forgotten, here's a little sermon from the grassroots: You're there because we're <i>paying</i> for you to be there. And more importantly, you're there because you're supposed to be a part of democracy in action. <p>
Democracy is dying. Freedom is dying. The middle class is dying. Torture is in. The Christian Right is replacing our Constitution (and judicial system) with their sitcom version of the Bible. Imbeciles are regressing science back into the Dark Ages. <p>
Dick Cheney's face is like something out of everyone's worst nightmare, and our brainless president has started a crusade against the entire Muslim world. And speaking of our cheerleader president, windows of opportunity are slamming shut as we speak for saving the life-and-death ecology of planet Earth.<p>
Hey, DNC dudes, the sky is falling! Wake up, wake up, wake up! Get out there and take some chances. Stop obsessing about your talk show profiles. Say something real. Stop blubbering your lips. Act like grownups. Show a little grit for once in your life.<br>
<b>W. Christopher Epler,</b> Albuquerque, N.M.<p>

