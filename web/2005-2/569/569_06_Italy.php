<font face="Times New Roman, Times, serif" size="5"><b>High-speed rail line sparks protests</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 16, 2005 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">THE CONSTRUCTION of a high-speed train line near Turin, Italy--the site of next year's Winter Olympics--has sparked a huge struggle and major clashes between demonstrators and riot police.<p>
Local residents and their supporters around the country believe plans for a 33-mile-long tunnel through the Alps will wreak environmental havoc. They fear that crews tunneling through asbestos and uranium deposits will cause long-term health risks and damage the picturesque mountain region--but that the train line is being built anyway in the interests of profit.<p>
Most of the Olympic events next February will take place outside Turin, in the nearby Val Di Susa--exactly where the high-speed train line to link Turin and Lyons in southern France will run.<p>
After several previous demonstrations, environmental activists occupied a site where construction firms planned to begin work. In early December, police descended on the occupation, using violence to drive out the activists.<p>
This sparked huge demonstrations of some 50,000 people, who battled riot police as they tried to reach the construction site. Despite firing volley after volley of tear gas, the police had to retreat.<p>
The police attacks sparked a general strike in the region, and blockades of rail lines and highways leading to both Olympic construction sites and nearby ski resorts. In Turin and other cities across the country, people gathered--mostly spontaneously--to protest the plans. A national demonstration is set for December 17 in Turin, and the local committees in the Turin region are discussing ways to protest the Olympic Games.<p>
Italy's right-wing Prime Minister Silvio Berlusconi backs the train project and dismisses the protests as the work of "1,000 far-left extremists." His crackdown also has support from governmental opposition parties like the Left Democrats.<p>
The scale of the unrest is so great that the head of the Italian organizing committee for the Winter Games called on the government to negotiate "a truce to save the Olympics."<p>
The struggle has united local residents with global justice organizations whose protests against the G8 summit in Genoa, Italy, in 2001 shook the country. They argue that there is--in the words of Italian transportation expert Marco Ponti--"no rational motivation" for building the Turin-to-Lyons link. "The capacity of the existing [rail] network is already excessive with respect to the demand, and goods that travel by rail have no need to move at 300 miles an hour," Ponti told the magazine <i>L'Espresso</i>.<p>
Yurii Colombo, an Italian socialist, told <i>Socialist Worker </i>that some of the demonstrators were from Italy's far-left groups, but most were local residents who were "more radical than us. The attempts of the right-wing government--and the center left--to say that this is only the far left isn't working."<p>

