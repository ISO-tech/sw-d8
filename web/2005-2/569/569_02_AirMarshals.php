<font face="Times New Roman, Times, serif" size="4">Air marshals shoot unarmed man in a Florida airport</font> <br>
<font face="Times New Roman, Times, serif" size="5"><b>Dead because he was mentally ill</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 16, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">RIGOBERTO ALPIZAR didn't have a chance. The 44-year-old, unarmed man with a bipolar disorder was shot to death December 7 in a Miami airport by air marshals who said they thought he was detonating a bomb.<p>
Alpizar had run off American Airlines Flight 924, which was waiting to leave the gate for a trip to Orlando, Fla.. He made it to the jetway connecting the plane to the airport concourse when marshals opened fire and killed him. No bomb was found.<p>
James Bauer, a special agent in charge of federal air marshals in Miami, said the marshals acted appropriately in response to Alpizar, who, they claim, "uttered threatening words that included a sentence to the effect that he had a bomb."<p>
Passengers on the flight, however, tell another story. "I don't think they needed to use deadly force with the guy," John McAlhany, a 44-year-old construction worker from Sebastian, Fla., told <i>Time </i>magazine. "He was getting off the plane." "I never heard the word 'bomb' on the plane," McAlhany said. "I never heard the word bomb until the FBI asked me did you hear the word bomb. That is ridiculous."<p>
Passengers say that Alpizar's wife, Anne Buechner, ran after her husband when he took off down the aisle toward the jetway. "She was running behind him, saying, 'He's sick. He's sick. He's ill. He's got a disorder," McAlhany said. "She was trying to explain to the marshals that he was ill. He just wanted to get off the plane." <p>
Another passenger, Alan Tirpak, told CNN, "She was just saying her husband was sick, her husband was sick...she just kept saying the same thing over and over, and that's when we heard the shots."<p>
McAlhany described the terrifying scene that followed, as armed marshals took over the plane. "They were pointing the guns directly at us instead of pointing them to the ground," he said. "One little girl was crying. There was a lady crying all the way to the hotel."<p>
Before Alpizar started to run off the plane, McAlhany said that he heard him arguing with his wife: "He was saying, 'I have to get off the plane.' She said, 'Calm down.'"<p>
Alpizar grabbed his backpack and ran off the plane. When marshals caught up with him and ordered him to lie down on the ground, Alpizar wasn't fast enough. Some witnesses say the marshals fired as many as six times.<p>
The White House immediately defended the marshals. "From what we know, the team of air marshals acted in a way that is consistent with the training that they have received," White House Press Secretary Scott McClellan told reporters.<p>
The two air marshals who killed Alpizar are among thousands hired after September 11, when the program of placing usually undercover agents on passenger flights was expanded from 33 to more than 6,000. Another marshal, who requested anonymity because they are forbidden to talk to reporters, told the <i>New York Times </i>that their rules for use of force were "basically same as any other law enforcement officer."<p>
That's bad news for people, like Rigoberto Alpizar, who have a mental disorder. According to the Treatment Advocacy Center, people with mental disorders are four times more likely to be killed by the police.<p>
It's also bad news for immigrants. Alpizar, who was originally from Costa Rica and received his U.S. citizenship several years ago, was described by neighbors in Maitland, a suburb of Orlando, as quiet and friendly. He worked as a paint salesman at Home Depot. When he was shot, he was returning from a trip with a church group to Quito, Ecuador.<p>
But, like Jean Charles de Menezes, the Brazilian immigrant shot by British police who believed he was responsible for the July subway bombings in London, Alpizar was guilty until proven innocent. This is the real face of "security" in Bush and Blair's war on terror.<p>

