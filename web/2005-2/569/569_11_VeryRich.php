<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Lifestyles of the rich and very rich</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 16, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">DO AMERICAN officials really believe their own lies? That crossed my mind when I read this statement by U.S. Army Col. Mark McKnight, who delivered a eulogy in November to commemorate the handing over of Saddam Hussein's largest Palace Complex to the Iraqi puppet regime.<p>
"The Iraqi people," he said, "will continue to move forward toward a sovereign Iraq, an Iraq where an elite few can no longer erect expensive and ornate palaces like these while the majority of the people suffer in fear and poverty." The whistling of the dud mortar overhead as he spoke--sending generals scrambling for cover--seemed a fitting catcall to this piece of hypocrisy.<p>
The ceremony involved a conquering power handing over a "symbolic key" to their Iraqi underlings. The entire proceedings, which were meant to highlight Iraq's steady move toward self-governance, merely underscored the master-servant relation between the two governments.<p>
But I want to focus on the part about erecting expensive palaces.<p>
Perhaps Col. McKnight wasn't aware of the fact that there are 341 billionaires in the U.S. whose combined assets now exceed a trillion dollars--that's a one with 12 zeros after it. On the top-ten list along with Bill Gates and Warren Buffet is Robson Walton, an heir of Sam Walton of Wal-Mart fame, a company notorious for paying its non-union employees low wages and lousy benefits.<p>
Perhaps he didn't know that a corporate CEO's average compensation now ranks at 431 times the average pay of a production worker. The biggest gains have gone to the CEOs of military contractors. According to a new Institute for Policy Studies report, their pay went up by 200 percent since 9/11.<p>
The top war profiteer was David Brooks, CEO of DBH Industries, who made a cool $70 million in direct compensation in 2004. He made even more by exercising his stock options. Before the company recalled 5,000 bulletproof vests after doubts about their effectiveness arose, Brooks sold $186 million in DBH stocks.<p>
And speaking of U.S. palaces, the following is from Richard Conniff's <i>A Natural History of the Rich: A Field Guide</i>: "When a New Yorker was building a 43,000-square-foot third or possibly fourth home on the ocean in Palm Beach, her main complaint was that the bedroom closet was so small. 'It's as big as my living room,' the architect protested, to which the owner replied, 'Why do you live in such a small house?'"<p>
Home developer Dwight Schar--your average, run-of-the-millionaire guy--spent $70 million on his 26,000-square-foot Palm Beach estate, which boasts 18 bathrooms, a movie theater, and a walk-in humidor.<p>
But why have only three or four land palaces when you can have flying and floating ones, too? <i>Business Week </i>reports that the rich are now spending as much as $125 million to buy their own personal wide-body passenger jets, "outfitting them with everything from his-and-hers bathrooms to onboard movie theaters."<p>
Microsoft cofounder Paul Allen owns a 414-foot yacht. "You really are nobody," a financial manager claims to have overheard the publisher of a yachting magazine say, "unless you have a full-sized basketball court in the back of your boat." Allen's has a lot more than that--including a garage, a movie theater, a concert hall and a recording studio.<p>
Meanwhile, the majority of Americans "suffer in fear and poverty." The latest outrage? The Supreme Court ruled on behalf of the Bush administration that the government can garnish Social Security checks to pay for delinquent student loans. According to an AP report: "The unanimous decision went against a disabled 67-year-old Seattle man who lives in public housing and had sued, claiming he needed all of his $874 monthly check to pay for food and medicine. James Lockhart's benefits had been cut by 15 percent to cover debts he incurred for college in the 1980s. He has about $77,000 in unpaid loans."<p>
What people like James Lockhart fail to understand is that their money is needed to pay military contractor CEOs like David Brooks.<p>

