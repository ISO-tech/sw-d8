<font face="Times New Roman, Times, serif" size="5"><b>Background to Stan's story</b></font> <p>
<font face="Arial, Helvetica, sans-serif" size="2">December 16, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">ELAINE BROWN is a former leader of the Black Panther Party for Self-Defense in the 1970s. This November, she ran for mayor of Brunswick, Ga., as the Green Party candidate. Elaine wrote this statement, giving historical background to the Stan's story, to be read at a November 30 screening of the movie <i>Redemption </i>in Berkeley, Calif.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">WHEN BUNCHY Carter founded the Southern California chapter of the Black Panther Party in early 1968 Los Angeles, gang activity in Los Angeles ceased. Bunchy had been the head of the powerful Slausons--predecessors to the Crips.<p>
In January 1969, Bunchy was assassinated by operatives of the FBI and in concert with the FBI. By that time, nonviolence had been killed, with the assassination of MLK. Eventually, the movement for freedom of that time was crushed by the FBI, and other state and police forces.<p>
The vacuum created by the absence of the party in L.A. was filled by agonizing despair, unchecked police brutality, ongoing oppression, poverty and, soon, drugs. The Crips rose as a response to rage--unorganized rage, but not unfounded. <p>
The police, particularly the LAPD, seized upon this righteous rage, and promoted and encouraged territorial divisions among the so-called gangs. Worse, the CIA financed the dumping of massive quantities of cheap cocaine into Black Los Angeles, creating a new and dangerous black economy--in a world in which less than 1 percent of all business revenues come from Black businesses. <p>
These were the factors and conditions that generated mass internecine violence attributed to the Crips.<p>
Gov. Schwarzenegger must recognize this. Taking Brother Tookie's life will not remedy harm that he never did. Let Brother Tookie live to speak--to speak out and use his life, as he has pledged, to raise the consciousness of young people locked in urban America's downward spiral..<p>
For my Brother Tookie Williams, let me say that I speak to you invoking the names of soldiers who were my comrades, George Jackson and Bunchy Carter and John Huggins and Fred Hampton, Bobby Hutton and Huey Newton--martyrs to the cause of our people's freedom. We know you, Brother. We love you. We're sorry we didn't change the world for you.<p>
Your voice is strong, though, and we know you will remain strong. We call upon you to let your influence be brought to bear to organize our young people to understand the nature of their oppression, to know who their oppressors are, who their true enemies are, and their true friends, and to move with the same urgency as in the past, as with the Crips, in a struggle for our freedom.<p>
Finally, I say, let us recognize that it is George Bush and his followers who must find redemption--and let us call forth the real criminals, the real murderers, who perpetrate war abroad and oppression at home who must be brought to justice. Long live Brother Tookie Williams!<p>

