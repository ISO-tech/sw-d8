<font face="Times New Roman, Times, serif" size="4">Jury rejects the government's case against Sami Al-Arian</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Victory for the Feds' witch-hunt victims</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 16, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">WITCH-HUNT victim Sami Al-Arian scored a major victory this month when a jury failed to convict him of a single charge following the government's five-month-long trial against him and three codefendants.<p>
Arrested in February 2003, the former University of South Florida professor and his codefendants--Hatem Fariz, Ghassan Ballut and Sameeh Hammoudeh--were on trial for charges including racketeering, conspiracy to kill civilians abroad and providing material aid for terrorists.<p>
But in the end, the jury acquitted Al-Arian of eight of the 17 charges against him, and deadlocked on the more minor charges in the case. Ballut and Hammoudeh, meanwhile, were acquitted on all charges, and Fariz was found not guilty on 24 out of 32 counts, with the jury deadlocked on the remaining eight.<p>
The government had alleged that the men were part of a Florida terrorist cell connected to the Palestinian Islamic Jihad (PIJ)--which the State Department declared a terrorist organization in 1995.<p>
Prosecutors whipped up a hysteria over the case--and presiding Judge James Moody seemed to allow it at every turn. For example, prosecutor Cherie Krigsman compared the PIJ to the Mafia, and tried to paint Al-Arian as a major figure in the group.<p>
From the beginning, Al-Arian--who had been investigated previously and cleared of the same charges--maintained that the only thing he was guilty of was speaking out in defense of the rights of Palestinians. Nevertheless, Moody allowed prosecutors to depict graphic bomb scenes and present the stories of family members of Israelis killed by suicide bombers--without ever establishing a connection between the violence and Al-Arian and his codefendants.<p>
At the same time, Moody refused to allow the defense to talk about or show scenes of the deaths of Palestinian civilians caused by Israeli military forces.<p>
As Al-Arian told journalist John Sugg in November, "There were 400 [wiretapped] conversations I wanted the jury to hear: Where I say this is not a religious war. My own words about Jews. My involvement with American politicians. In one conversation, June 19, 2002, I condemned [a terrorist attack]. But I can't bring that into court. Moody wouldn't allow that."<p>
Yet in the end, the jury rejected the prosecution's claim that its evidence--thousands of hours of phone conversations gained through secret wiretaps authorized under the USA PATRIOT Act--showed the men's involvement in terrorism.<p>
Despite spending more than 1,000 days in prison and enduring extended periods of solitary confinement, periodic lack of access to his lawyers, wife and children, and inadequate medical treatment, Al-Arian's ordeal is far from over.<p>
While the failure of the prosecution to win a conviction is a major victory, Al-Arian and Fariz remain in prison indefinitely, until the government decides whether to retry them on the charges that the jury deadlocked on. Even if the government decides not to retry Al-Arian, the Department of Immigration and Customs Enforcement (ICE) announced that it will seek to have him deported.<p>
Despite Hammoudeh being acquitted of all charges, ICE agents detained him immediately following the conclusion of the trial. He is now scheduled to be deported in late December.<p>
According to federal immigration officials, there is sufficient cause to deport Al-Arian as well. But this "cause" includes not only his unproven connection to the PIJ, but allegations that he committed "voter fraud"--stemming from when Al-Arian registered and voted in 1994, despite the fact that he was not a citizen.<p>
The move to deport Al-Arian may be an excuse to simply keep him behind bars for years to come. Although he was born in Kuwait and grew up in Egypt, as a Palestinian, Al-Arian is technically not a citizen of either country--which means that he could remain in U.S. custody indefinitely.<p>
As Sugg commented, "This case was entirely an attack on Constitutional rights, especially the First Amendment. The government of Israel wanted Al-Arian silenced, and our government obliged."<p>

