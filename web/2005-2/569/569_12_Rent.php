<font face="Times New Roman, Times, serif" size="5"><b><i>Rent</i>--the ghost of East Village past</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Jennifer Roesch</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 16, 2005 | Page 12</font><p>

<b><i><font face="Arial, Helvetica, sans-serif" size="2">Rent, </b></i>directed by Chris Columbus, starring Rosario Dawson and Taye Diggs.</font><p>

<font face="Times New Roman, Times, serif" size="3">THE SCREEN adaptation of the 1996 rock-opera <i>Rent</i> captures a particular moment in history (the height of the domestic AIDS crisis during the Reagan-Bush era) in a particular place (New York City's East Village) with startling intensity.<p>
<i>Rent</i> opens on Christmas Eve 1989 with a riot over planned evictions of squatters and homeless people from Tompkins Square Park and the surrounding area.<p>
In reality, there were riots in August 1988 when police moved in to evict the large number of homeless people living in the park. This is an East Village where Polish and Latino immigrants live alongside bohemian artists, activists, squatters and drug dealers.<p>
But the attempted evictions are the beginning of a gentrification process that has since turned the neighborhood into a yuppie haven booming with expensive restaurants and high-priced renovated apartments. The rebellion against authority that characterized the East Village in the late 1980s has been reduced to a fashion statement--faux grunge items at $100 a piece.<p>
For anyone who lived in the neighborhood during this time, one of the most poignant things about watching the film is seeing a world that has been largely erased. Following the lives of seven friends, <i>Rent</i> is in many ways a celebration of that world and its spirit of non-conformity and resistance.<p>
The multiracial group of friends, half of whom are HIV-positive and an equal number of whom are gay, form an extended family whose bonds are stronger than most conventional ones. As one of the songs proclaims, "When you're dying in America at the end of the millennium, you're not alone."<p>
This statement rings all too true for a time when whole communities were torn apart by AIDS.<p>
One of the most touching relationships is between African American, HIV-positive Collins and Latina, HIV-positive drag queen Angel. The depiction of the committed, tender relationship between these two, one that lasts through Angel's death from AIDS, flies in the face of stereotypes.<p>
<i>Rent</i> is able to accurately capture the contradictory reality of finding love, friendship and art in a time and place dominated by poverty, drug addiction and death. All of the characters struggle with their own demons but are able in some measure to overcome the inherent alienation in our society through the community they form.<br>
One of the most vibrant numbers in the film, "La Vie Boheme," is a joyous tribute to, in its words, "the need to express, to communicate, to going against the grain...to more than one dimension, hating convention, hating pretension (not to mention, hating dear old mom and dad)."<p>
In a society that simultaneously exalts the nuclear family while commercializing "alternative lifestyles," <i>Rent</i> strikes a blow against stultifying conformity.<p>

