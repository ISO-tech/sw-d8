<font face="Times New Roman, Times, serif" size="5"><b>Stepping up the fight at HCC</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 16, 2005 | Page 10</font><p>

<font face="Times New Roman, Times, serif" size="3">WITH THE Supreme Court set to hear <i>FAIR v. Rumsfeld </i>to determine the constitutionality of the Solomon Amendment, the Holyoke Community College (HCC) Anti-War Coalition (AWC) organized a community forum to discuss the atmosphere on campus and to question the college president about actions taken against students who exercise their constitutional rights. <p>
Simultaneously, the AWC rotated its members out of the forum to confront Army recruiters who were camped out at a "Career Services Department" at the same time.<p>
Following the events of September 29, when HCC student Charles Peterson was maced by campus police during a counter-recruitment demonstration protesting the Army National Guard, the AWC has stepped up the level of struggle and increased political awareness on campus. <p>
Military recruiters have shown up three times since the attack on students in September. On two consecutive occasions, a recruiter from both the Marines and the Army attempted to recruit students without notifying college administrators. The other time was scheduled, but unsuccessful, due to the more than a dozen antiwar students who filled the hall waiting to question recruiters--thus wasting their time and making them uncomfortable and unwelcome.<p>
In the event of a violation of school policy brought to administrators by students, the grievant is passed on to someone else who is "responsible" (typical of a bureaucracy) and subsequently sent in circles. In many cases, there is no formal written policy, and a mere "practice" prevents anyone from being held accountable. Meanwhile, students are accountable for every minor violation of school policy. <p>
The double standard for administrators is unacceptable. The Open Campus Community Discussion with President William Messner held on November 23 was the place for accountability. <p>
President Messner "seemed totally unprepared," as one student pointed out after another asked Messner, "Why haven't you answered any of the questions people are asking you?" <p>
Many faculty, staff and students raised great comments and questions that asked about the administration's accountability was and how the college could better serve its community given the increasing resistance to military recruiters nationwide. One woman spoke of her brother not understanding what he was signing up for when he joined the military--adding "and he's a lawyer!"<p>
Attendance at the event included members of the College Republicans and officer Scott Landry, the cop responsible for attacking AWC members and macing Charles Peterson. The College Republicans and police all sat together. Interestingly, Landry and another officer purchased copies of <i>College Not Combat</i>--the Campus Antiwar Networks' newspaper--before entering the forum.<p>
Many community and faculty members applauded the AWC's well-organized event, including College Republican President Kevin Orzechowski, who then went on to question if the AWC should receive school funds because it supports the Iraqi resistance. It seemed clear that the audience didn't approve of his allegations claiming the AWC supports terrorism. Mr. Orzechowski left the forum after his question.<p>
Immediately after wrapping up the forum, AWC members proceeded over to the "Career Services Department," where they questioned Army recruiters. Students asked questions like, "What does the Army think about the use of napalm and white phosphorus in Falluja?" and "Is the Army concerned about human rights?" until the soldiers refused to answer any more questions. At that point, the Army packed its bags.<p>
As the AWC escorted the recruiters to their vehicle (marching with banner in hand), members made it a point to show their respect to the recruiters as human beings, but not as soldiers of an imperialist army.<p>
With the U.S. military growing ever more desperate for warm bodies to fight its illegal war for the rich, HCC will continue to see effective organizing from the Anti-War Coalition. The open forum, counter-recruitment efforts, and constant organizing have brought the war into focus at HCC. <p>
Political awareness is rising all around us, and the counter-recruitment movement in schools across the country is playing a large role. Students will continue to organize and fight back so long as there is continued U.S. imperialist oppression at home and abroad. Contact your local CAN chapter at <a href="http://www.campusantiwar.net" target="_blank">www.campusantiwar.net</a> to find out how you can help.<p>
<b>Barry Scott, </b>Holyoke, Mass.<p>

