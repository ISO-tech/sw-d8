<p><a name="Up"></a><font face="Times New Roman, Times, serif" size="4"><b>Stand up for immigrant rights</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Nestor Casas</b></font><p>

<font face="Times New Roman, Times, serif" size="3">GLENDALE, Calif.--The Glendale Labor Center was the site of the most recent offensive against immigrants by the vigilante group Save Our State (SOS). <p>
In response to a December 10 SOS rally, a few dozen counter-protesters--including day laborers, immigrant rights groups, the ISO and others--came out to show their support for immigrants and challenge the racism of SOS. SOS has been targeting labor centers across Southern California to whip up anti-immigrant sentiment.<p>
Passing cars honked in support of immigrant rights, and with our rally dwarfing the weak turnout of SOS supporters, Glendale was the latest scene of the defeat of the SOS. The victory was in no small part due to the successful and significant coalition of day laborers, immigrant rights groups and other activists.<p>
It is essential that bigots face opposition everywhere they go and that their far-right views are pushed back into the gutter where they belong.<p>

