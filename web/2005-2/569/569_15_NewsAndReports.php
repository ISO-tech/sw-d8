<font face="Times New Roman, Times, serif" size="4"><b>Military out of our schools</b></font><p>

<font face="Times New Roman, Times, serif" size="3">STUDENTS AND antiwar activists turned out in cities across the U.S. on December 6 to protest the presence of military recruiters on college campuses.<p>
The actions, part of a national day of counter-recruitment called by the Campus Antiwar Network (CAN), coincided with the opening of arguments in <i>FAIR v. Rumsfeld</i>, a Supreme Court case brought by several universities which will decide whether schools can ban military recruiters (and uphold their own anti-discrimination policies) without losing federal funding. The day of action was endorsed by prominent activists, including Cindy Sheehan, Howard Zinn, Dahr Jamail, war resisters Pablo Paredes and Camilo Mej&iacute;a and more.<p>
-- In <b>Washington, D.C., </b>activists traveled from as far away as Chicago to protest in front of the Supreme Court. Counter-recruitment activists Tariq Khan from George Mason University, John Robinson from Hampton University, Angela Kelly from the D.C. Antiwar Network and many more spoke about the importance of the movement and its victories--including the passage of Proposition I ("College Not Combat") in San Francisco, and a recent University of California-Davis decision to ban military recruiters from campus.<p>
-- In <b>San Francisco, </b>a diverse crowd of high school and college students, teachers, community activists and the Raging Grannies gathered at the Ferry Plaza to listen to speakers that included conscientious objector Aimee Alison, Proposition I co-author Todd Chretien, and representatives of CAN. The group of 50 then marched and picketed at a military recruitment center.<p>
-- In <b>Boston, </b>more than 80 people--including high school students, and members of the Stop the Wars Coalition and the Raging Grannies--protested outside a downtown military recruitment center. When one recruiter shouted at a gay high school activist that "I do not kill people--except pieces of shit like you!" a crowd gathered to loudly call the recruiter out for being a bigot.<p>
-- In <b>Chicago, </b>approximately 50 students, teachers, war veterans and gay rights and antiwar activists gathered in Federal Plaza in single-digit weather. Speakers at the protest included Gold Star Families for Peace member Juan Torres and Eric Ahlberg, a veteran of the Iraq and Afghanistan wars.<p>
-- In <b>New York City, </b>dozens of activists protested outside a recruiting station near the Borough of Manhattan Community College. Speakers included Victor Paredes, the brother of war resister Pablo Paredes, CAN National Coordinating Committee member Elizabeth Wrigley-Field, and Nicholas Bergreen, who was assaulted by police and suspended from City College New York for staging a peaceful counter-recruitment protest. <p>
-- In <b>Ithaca, N.Y., </b>more than 30 people--including students from Cornell University, Wells College and Ithaca College, as well as community members--gathered for a demonstration outside a military recruiting station. The recruiters locked their doors as the crowd rallied, chanting "What do we want? Troops out <i>now</i>!"<p>
<i>Rayyan Ghuma, Julie Keefe, Kristin Lubbert and Bekah Ward contributed to this report</i>.<p>

