<p><a name="DeLay"></a><font face="Times New Roman, Times, serif" size="4"><b>Put the Hammer in the slammer</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Folko Mueller</b></font><p>

<font face="Times New Roman, Times, serif" size="3">HOUSTON--About 300 people representing more than 20 organizations gathered December 5 outside a campaign fundraiser for Tom DeLay, which included an appearance by Dick Cheney.<p>
Both the "Hammer," as DeLay is nicknamed for his rants against political opponents, and Cheney must have thought they'd be on home turf here in the alleged heart of "Bush country." But as the Republican faithful were doling out $500 a piece for dinner and up to $4,200 for a photo with Cheney, the chants outside rang clear. "Put the Hammer in the slammer!" "You will pay, Tom DeLay!" "Support our troops, bring them home now!" The DeLay supporters at the hotel were apparently well aware of what was going on outside. <p>
Several activists accompanied Diane Wilson of Code Pink into the fundraiser as she unfurled a banner and then came out to inform us that DeLay not only criticized the protest, but made references to a couple of organizations by name, including the ISO. The protest was reported in the local media as well as on Michael Moore's Web site and in the Spanish-language press.<p>
Amusingly, a DeLay spokesperson claimed that our event was organized by "professional protesters" from San Francisco--which only goes to show how detached from reality DeLay and his people are. They just can't stomach any kind of opposition to their mad policies this close to their constituencies. <p>
The tide has begun to turn--in the U.S. and in Texas--but we have to keep organizing. This is the only way we can be sure that these politicians will one day end up where they belong--in the slammer.<p>

