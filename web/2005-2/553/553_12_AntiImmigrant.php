<font face="Times New Roman, Times, serif" size="4">Anti-immigrant racists go on the offensive in Calif.</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Standing up to the bigots</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jocelyn Blake and Nohelia Ramos</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 19, 2005 | Page 12</font> <p>

<font face="Times New Roman, Times, serif" size="3">ANTI-IMMIGRANT bigots are determined to take the offensive in southern California. But opponents of racism and injustice are equally determined to confront them wherever they try to spew their hate.<p>
In early August, anti-immigrant vigilantes who mobilized to "patrol" the border with Mexico in the desert area east of San Diego ended their three-week Border Watch early. Around 70 pro-immigrant rights protesters held a "good riddance party" to celebrate the racists' departure, marching to the Veterans of Foreign Wars hall in Campo, Calif., which the vigilantes had used as a rallying point. Most of the bigots were already gone--because they took down their camp and left the border earlier than planned. <p>
Now, immigrant-rights supporters are preparing to counter another vigilante border action called for September 16.<p>
On August 11, more than 450 people jammed into a "town hall" meeting in Carlsbad, Calif., north organized by Republican state Sen. Bill Morrow. Morrow stacked the speakers' platform with anti-immigrant racists, including Rep. Tom Tancredo (R-Colo.), a hero of the bigots nationwide, and Jim Gilchrist, founder of the Minuteman Project, which grabbed headlines when it invaded Arizona earlier this year to "patrol" the border.<p>
Almost everyone who squeezed into the meeting was an opponent of immigrant rights. They turned out in large numbers after emcee Roger Hedgecock, a right-wing radio host, told listeners to his show that they should arrive early to fill up the auditorium.<p>
Outside the forum, held at the Carlsbad High School, more than 150 people--including Latino activists, Muslim students and members of the immigrant rights coalition Gente Unida--denounced Morrow's staged meeting.<p>
The Muslim counter-protesters spoke out in particular against Tancredo, who recently declared that Islamic mosques and schools "are simply breeding grounds for future terrorists" and that the U.S. should bomb "their holy sites" if there is another terrorist attack in the U.S. Tancredo's anti-Arab diatribe shows that the racism of vigilante groups like the Minutemen isn't limited to immigrants.<p>
Outside the meeting, Muslim and Latino activists chanted, "Racists go home!" and "Los inmigrantes no son terroristas!"--and vowed to return to challenge the bigots in the future.<p>

