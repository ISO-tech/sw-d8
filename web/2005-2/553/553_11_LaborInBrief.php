<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">August 19, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>New York University</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Sarah Wolf, GSOC</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--The New York University administration issued a "final decision" stating that it will not negotiate with the Graduate Student Organizing Committee/United Auto Workers Local 2110, the graduate student union here.<p>
Adding insult to injury, this decision was supposedly based on the union's rejection of an administration "proposal" that we agree to a "contract" whose terms would be wholly set by the university. These terms included the elimination of all grievance procedures, the turning of NYU into an open shop, and the possibility of changes to the content of our health care plan at the university's discretion.<p>
After a National Labor Relations Board ruling two years ago, graduate student teachers and research assistants are no longer considered "workers"--despite the fact that if we did not teach, the university would not run. Hence, NYU is no longer legally obligated to recognize the union that student teachers won four years ago here in an unprecedented victory at a private university.<p>
This decision, however, hasn't been and won't be met lying down. In fact, it came in the wake of several indications that a majority of teaching assistants and research assistants support the union, including a pro-union petition signed by 800 grad students, a hundreds-strong demonstration in the spring by union members and supporters, and a university-sponsored "Town Hall Meeting" at which all but a handful of the 200 students, faculty and staff in attendance expressed support for the union.<p>
Our pressure will only mount as we begin the new academic term without our contract, which runs out on August 3.<p>
The union will have a visible presence at the August 30 "New Teaching Assistant Training" day. The next day, a rally will draw members, supporters around the city and graduate students at nearby universities, including Yale and Columbia (where unionization campaigns remain underway), to demand that the university negotiate a new contract.<p>
Ultimately, however, our union has taken the position that the university will likely not cave to any pressure less than that of a strike, and we have launched a phone campaign amongst members to argue for the necessity of such an action. So far, those students that we have talked with have been supportive of this idea.<p>
Despite this agreement on tactics, however, more disagreement does exist among organizers about what sort of general strategy will force the university to negotiate. Questions have arisen at recent organizing meetings, for instance, about whether we should agree to the university's requests for behind-the-scenes discussions with "higher-up" union representatives in order to retain the moral high ground.<p>
Judging by NYU's recent full-fledged, but unsuccessful, campaign of propaganda and threats to try to stop adjunct professors from forming a union, winning a contract for graduate students will require stepping up the fight.<p>

