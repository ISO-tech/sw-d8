<font face="Times New Roman, Times, serif" size="5"><b>Beaten by racist thugs in New York</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nick Bergreen and Brian Jones</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 19, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--"I'm going to get you, you Black nigger!" an attacker yelled as he and other white men jumped on Alex Moore, an African American man walking with a friend in Brooklyn August 8. <p>
Nine-year-old Jeffrey Romasanta, who witnessed the attack, said six white men jumped out of a van or SUV and "were kicking [Moore] in the stomach, [as] he was screaming for help...There were bats, chains and a hockey stick. They threw him to the [ground] and started kicking him, and they were hitting him with bats and chains."<p>
A taxi drove by as the beating was taking place. The driver and passengers got out of the cab and yelled at the racist thugs to go away, which they did. According to Brookdale Hospital, Moore suffered a deep chest bruise, a laceration on one of his ankles and a hip contusion.<p>
The assault took place less than six weeks after a strikingly similar racist attack in the New York City neighborhood of Howard Beach, Queens. Then, four white men yelling racial slurs jumped out of an SUV, armed with metal pipes and bats, and attacked three Black men. The four fractured the skull of Glen Moore (no relation to Alex Moore) in an attack that recalled the racist murder of a Black man in the same neighborhood in 1986.<p>
New York City Mayor Michael Bloomberg, seeking re-election this fall, condemned the attacks, but claimed that hate crimes were down 50 percent overall. In fact, the attacks took place in an increasingly racist atmosphere encouraged by Bloomberg and other politicians to justify searches of passengers' bags in subways, a practice begun following the July bombings in London. <p>
Soon after, five Sikh men on a sightseeing tour in New York were seized by police, handcuffed and forced to kneel on the street while they were questioned--all because the tour bus driver thought the men, who wear turbans, looked "suspicious."  <p>
And on August 1, two New York City politicians--State Assemblyman Dov Hikind, a Democrat, and City Council member James Oddo, a Republican--called for ethnic and racial profiling of Arab and South Asian men in the subway searches. According to Hikind, potential terrorists "all look a certain way."<p>
Jan Clausen, a member of Prospect Lefferts Voices for Peace, a Brooklyn-based activist organization, said the racist attacks were tied up with calls for racial profiling. "In our activist work, our peace work and anti-imperialist work, our group sees a lot of connections between things that happen on the local level and what happens internationally."<p>
Racial profiling is already standard operating procedure for the New York Police Department. In a three-day period beginning June 14, police rounded up 181 African American men in Queens, making 93 misdemeanor arrests the first day--10 times the normal amount.<p>
Deputy Police Commissioner Paul Browne defended the random roundup, saying "We sought to determine whether the persons arrested would have any information about the shooting, and that's what we should be doing." But the "shooting" Browne referred to occurred when a police officer in the 105th precinct shot himself in the foot with his own gun--while trying, unsuccessfully, to apprehend a man in a park for possession of marijuana.<p>
Angela, a resident of the Brooklyn neighborhood where Alex Moore was assaulted, made a connection between the NYPD's harassment of Black men and the attack on Moore. "The police do it, so that sends the message that it's okay for them to do it, too," she said. "[The police] need to clean up their act before we can feel safe. They're like a gang, an organized gang running around here. They intimidate people because they think they can't speak up for themselves."<p>

