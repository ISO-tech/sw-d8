<font face="Times New Roman, Times, serif" size="5"><b>Defending choice at the clinic doors</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 19, 2005 | Page 5</font><p>

<font face="Times New Roman, Times, serif" size="3">VIOLENCE AGAINST clinics that provide abortion services was in the spotlight last week after NARAL Pro-Choice America pulled an ad opposing Supreme Court nominee John Roberts.<p>
The ad featured Emily Lyons, a worker at an Alabama abortion clinic who was severely disfigured in a 1997 bomb blast that also killed a security guard at the clinic. The spot attempted to link Roberts with anti-abortion forces that terrorize clinics, citing Roberts' 1991 co-authoring of a legal brief that sided with Operation Rescue and other groups that blockaded clinics.<p>
Condemnation of the ad was immediate and scathing--not only from conservative supporters of Roberts, but from liberals as well. "In general, discussion of the nomination of Judge John G. Roberts Jr. to the Supreme Court has taken place on a civilized level," a <i>Washington Post</i> editorial said. "Democratic senators, by and large, have appropriately reserved judgment; disputes over documents, while pointed, have been polite. The ad released this week by NARAL Pro-Choice America is a distressing exception."<p>
But what's really "distressing" is the Democrats' far-too-polite approach to the prospect of a hard-line conservative like Roberts sitting on the U.S. Supreme Court.<p>
Still more distressing is what Washington's non-debate has masked over--the very real threat of violence that staff and patients face at abortion clinics. <p>
A chilling reminder of this was the sentencing this month of Eric Rudolph, who was responsible for setting the bomb that maimed Lyons. "What they did was participate in the murder and dismemberment of upward of 50 children a week," Rudolph said of the clinic staff in a court statement. "Abortion is murder," Rudolph said, adding, "I believe that deadly force is indeed justified in an attempt to stop it."<p>
Abortion providers are under attack. Since 1977, more than 2,400 acts of violence have been reported against providers, including bombings, death threats, arson, kidnappings, assaults and murder, according to 2001 statistics from NARAL.<p>
With anti-abortionists setting this price on their heads, is it any wonder that 87 percent of U.S. counties had no abortion provider, as of 2000?<p>
How can abortion-rights supporters defend women's legal right to abortion services without fear of violence or harassment? If the narrow confines of the "debate" over Roberts are any indication, the makeup of Supreme Court, by itself, is hardly a guarantee that clinic access will be preserved.<p>
At the clinics themselves, across the country, right-wing Christian groups like the "Crusade for Life" are organizing pickets.<p>
In far too many cases, liberal pro-choice groups--which have long have focused on activities, like lobbying, that won't scare away politicians and wealthy donors--are not only failing to confront the bigots, but are arguing against organizing. For example, a Planned Parenthood facility in San Francisco that was the target of anti-abortion pickets circulated a letter in July calling on some 40 pro-choice protesters who came out to counter the bigots to stop their defense of the clinic.<p>
Abortion-rights activists aren't the problem. It is abortion opponents who are set on creating an atmosphere where it is harrowing--or impossible--for a woman to enter a clinic.<p>
The way to challenge them is to organize pro-choice forces to confront them when they picket the clinics--and make sure they think again before they decide to attack another clinic.<p>

