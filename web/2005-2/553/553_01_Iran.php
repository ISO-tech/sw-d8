<font face="Times New Roman, Times, serif" size="5"><b>Bush escalates war threats against Iran</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 19, 2005 | Pages 1 and 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE BUSH administration denounced the Iranian government as a "threat to peace" for restarting a uranium enrichment operation that the U.S. claims will be used to make weapons.<p>
But a conservative U.S. magazine is reporting that the real threat is coming from Washington--with Dick Cheney ordering the Pentagon to develop new plans for a military strike against Iran, including the use of "bunker-busting" nuclear weapons.<p>
U.S. rhetoric against the Iranian government, led by newly inaugurated President Mahmoud Ahmadinejad, reached a fever pitch after Iran resumed work at a key nuclear facility, ending a self-imposed ban on uranium conversion while it negotiated with a group of European countries. Bush's new Ambassador to the United Nations John Bolton claimed that "Iran will have nuclear weapons" soon as a result.<p>
But the U.S. government's spy agencies think differently. Anonymous sources told a <i>Washington Post </i>reporter that the most recent National Intelligence Estimate on Iran concluded that it is 10 years away from obtaining "the key ingredient for a nuclear weapon." United Nations (UN) inspectors haven't reported any concrete evidence that Iran has been conducting a nuclear weapons program, or that it has a design for a warhead.<p>
The same can't be said for nearby India or Pakistan--a newly converted U.S. ally in the U.S. "war on terror"--but Bush isn't denouncing them as "threats to peace." Both countries, like Washington's favorite ally, Israel, have refused to sign the international nuclear non-proliferation treaty. Iran, on the other hand, is a signer, which is why UN inspectors have current information on the country's nuclear program.<p>
Bush said last week that he might use military force if Iran didn't halt its nuclear program, but a report in Pat Buchanan's <i>American Conservative </i>magazine showed that plans for an attack are further advanced than his administration is admitting.<p>
According to an <i>American Conservative </i>columnist, the Pentagon is under orders from Dick Cheney's office to develop plans for a retaliatory attack on Iran in the aftermath of "another 9-11-style attack." The large-scale assault would use both conventional and nuclear weapons, wrote former CIA officer Philip Giraldi, against some 450 strategic targets. Many of the targets are in populated areas, guaranteeing massive casualties.<p>
"As in the case of Iraq, the response is not conditional on Iran actually being involved in the act of terrorism directed against the United States," Giraldi wrote. <p>
Bush administration threats against Iran aren't new. Throughout the buildup to invasion of neighboring Iraq, the White House's neo-conservative "hawks" hinted that Iran would come next. "Everyone wants to go to Baghdad," an anonymous administration official told <i>Newsweek </i>in August 2002. "Real men want to go to Tehran."<p>
But the neo-con plan to remake the Middle East has run into obstacles. In particular, the guerrilla resistance to the U.S. occupation of Iraq is stretching the U.S. military to its limits, producing opposition within the Pentagon to plans to add another front to the "war on terror." <p>
"In a historic irony," wrote historian and Iraq expert Juan Cole, "Iran's most dangerous enemy of all, the United States, invaded Iran's neighbor with an eye to eventually toppling the Tehran regime--but succeeded only in defeating itself."<p>
The threat of a U.S. attack is far from over, however. With Bush's approval ratings plunging as the crisis of the Iraq occupation deepens, the desperate hawks could try to force through a new attack--perhaps limited to air strikes on supposed "nuclear" targets, but potentially involving ground forces--and expand the horror and suffering of their "war on terror."<p>

