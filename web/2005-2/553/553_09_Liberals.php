<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Should we trust the liberal elite?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 19, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">THE FOUNDING congress of the Russian socialist movement in 1898 noted, "The further east one goes in Europe, the meaner, more cowardly and politically weak the bourgeoisie becomes, and the greater are the cultural and political tasks that fall to the proletariat." This later became the foundation of Lenin's approach to Russia's revolutionary prospects.<p>
Though most Russian socialists agreed that the most pressing task in Russia was the overthrow of autocracy--that is, a revolution to replace the Tsarist state, redistribute land and establish a constitutional democracy--there was sharp disagreement over what social and class forces would accomplish this task.<p>
Lenin concluded very early that the Russian liberal Cadet party--the political representatives of the Russia bourgeoisie--could not be trusted to support "their own" revolution. "The bourgeoisie as a whole is incapable of waging a determined struggle against the autocracy," he wrote. "It fears to lose in this struggle its property which binds it to the existing order...For this reason, the bourgeois struggle for liberty is notoriously timorous, inconsistent and half-hearted."<p>
From this, the two main wings of the socialist movement, Bolshevik and Menshevik, drew different conclusions.<p>
The Bolshevik wing concluded that it must build a working-class movement, in alliance with rebellious peasants, which retained complete independence from the liberals, and was prepared to push past them as soon as they began, as they inevitably would, to vacillate and turn on the movement.<p>
Indeed, Lenin argued that the more successful and more sweeping the workers and peasant struggle against Tsarism became, the more the bourgeoisie would recoil from it. "The Cadets are a party of the liberal bourgeoisie," wrote Lenin. "The economic position of that class makes it <i>afraid</i> of a peasant victory and of working-class solidarity. This accounts for the inevitable, and by no means fortuitous, tendency of the Cadets to turn the more rapidly to the right, to turn towards a deal with reaction, the more rapidly the popular masses turn to the left."<p>
The Mensheviks drew an opposite conclusion. Since Russia's revolution must be a "bourgeois revolution," wrote one of its leaders Martynov, "it is clear that the coming revolution can on no account assume political forms <i>against the will of the whole</i> of the bourgeoisie...If so, then to follow the path of simply <i>frightening</i> the majority of the bourgeois elements would mean that the revolutionary struggle of the proletariat could lead to only one result--the restoration of absolutism in its original form."<p>
The Mensheviks set as their main task, therefore, the policing of the revolutionary movement to ensure that it restrained its own actions and demands within bounds acceptable to the bourgeoisie. That meant that workers shouldn't seize control of the factories, and peasants shouldn't seize the landlord's estates.<p>
The Mensheviks accused the Bolsheviks of being sectarian splitters, whose radicalism and intransigence toward liberals threatened to split the movement.<p>
In response, Lenin argued that one must be prepared to "divide" from forces that are prepared to stand in the way of the struggle. Unity for Lenin was for the purposes of advancing the struggle, not restraining or derailing it. "We Social-Democrats have a different task--that of accelerating the liberation of the masses from the sway of the Cadets," he wrote.<p>
Despite the obviously different conditions and circumstances, these debates offer today's socialists invaluable lessons. In the United States, the liberal bourgeois party--the Democrats--represents a class that has been in power for centuries. So obviously, the issue of the role of liberalism in the struggle takes on a different hue.<p>
The Democrats are a party that openly identifies with America's imperial goals and the interests of big capital. Yet we continue to face political forces on the left who, like the Mensheviks, insist that we must unite with liberal Democrats and act carefully so as not to jeopardize that alliance--in order to "defeat the right wing."<p>
This is, however, as it has always been, an embrace that, instead of advancing the struggle, smothers it.<p>

