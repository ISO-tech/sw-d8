<font face="Times New Roman, Times, serif" size="5"><b>UAW workers push back at NUMMI</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Caroline Lund, Trustee, UAW Local 2244</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 19, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">FREMONT, Calif.--Workers at the New United Motor Manufacturing, Inc. (NUMMI) auto plant voted August 9 to ratify a new contract, with 80 percent in favor. This was the hardest-fought contract struggle in the 20-year history of UAW Local 2244, which represents workers at the company, a joint venture of GM and Toyota.<p>
Despite the plant's reputation as a showcase for labor-management cooperation, NUMMI negotiators demanded unheard-of concessions, up until the day before the contract expiration date.<p>
The management demands included a five-year contract with no raises, payment of 30 percent of our health insurance premiums (we paid nothing before), 30 percent of health insurance premiums also for retirees, and a permanent two-tier wage and benefit system starting new hires at $15 per hour and going up to $18.50 per hour after five years. Top pay under our old contract was $26 per hour. And for the first time, management refused to pay the wages of union negotiators during bargaining.<p>
In mid-July, the Local 2244 bargaining committee put out a flyer in the plant informing the membership of management's outrageous demands. The bargaining committee is part of a new leadership team elected two years ago when they ran against the in-plant supporters of the UAW International leadership's Administration Caucus.<p>
As the August 6 deadline approached and management did not budge, the bargaining committee began to seriously prepare for a strike. In the two days before the deadline, a big chunk of Local 2244's 4,000-plus members signed up for picket assignments. Arrangements were made for the Alameda County Central Labor Council to garner support from all other unions involved with NUMMI.<p>
Hundreds of members came out August 6 to picket the plant with hand-made signs, and hung signs on the freeway overpass near the plant. We got on the TV news on at least three channels. Some of the picket signs read: "I thought this was NUMMI, not screw me," "No raise for five years?" "Slave driver!" and "I can't be a temp forever." The latter sign referred to NUMMI's introduction of temporary workers over the past year.<p>
Under pressure from the bad publicity and the serious strike preparations, NUMMI backed down on their demands at 10:30 p.m. that night.<p>
The four-year contract just approved by the membership includes: a $3,000 signing bonus and $1.61 cost-of-living allowance fold-in the first year, a 3 percent lump sum the second year, a 2 percent wage increase the third year, and a 3 percent wage increase the fourth year. <p>
Our medical co-pays will rise from $5 to $10 for a prescription and from $10 to $15 for a doctor visit, but the demand for premium co-pays was dropped. Pensions will rise from $47 per month for each year of service to $51 over the four years. There will be no permanent two-tier wage, but the company will be allowed to work temps up to 180 days. Current temps are supposed to be able to fill any permanent openings.<p>
When you consider all the concessions various unions have been taking in recent years, the NUMMI contract does not seem so bad.<p>
The worst concession, I believe, is the green light for temps, who were accepted over the past year by the union as an alternative to management threats of outsourcing hundreds of jobs. We have experienced how damaging it is to have second-class union members who do the same work we do, but with no benefits, raises or job security.<p>
In a step forward, our bargaining committee gave members one day to look over the tentative contract highlights prior to voting on it on a workday. In the past we were giving the highlights at a Sunday meeting and had to vote immediately.<p>

