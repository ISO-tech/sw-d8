<font face="Times New Roman, Times, serif" size="4"><b>A new name, a bigger war</b></font><p>

<font face="Times New Roman, Times, serif" size="3">"THE WAR on terror" is over! Now the militaristic, racist campaign against civil liberties will be called "the global struggle against violent extremism." This change in terminology was started in mid-July by Defense Secretary Donald Rumsfeld and was followed soon after by Bush.<p>
Why the change? Some commentators say the "war on terror" was losing its punch. Military officers say it made people think the fight against terrorism was only military. To get more people identifying with the government's campaign, the name had to change. <p>
But the change is more sinister than just a PR move. Government officials are stressing that the enemy is not just a method (terrorism), but an ideology, "violent extremism." This means that the enemy is a set of ideas. With this rationale, it will be easier to target people for their thoughts, for advocating "extremism." <p>
We've already seen this recently with the trial of a Muslim cleric for "supporting" terrorism in words. This change fits nicely with the new McCarthyism on college campuses.<p>
This shift is also an attempt to position the Bush Administration in the middle of the political spectrum. A regime that has killed 100,000 Iraqis, openly advocated pre-emptive war, gutted social spending, attacked labor, undermined worker safety, presided over millions of job losses and declining living standards, tried to destroy abortion rights, attacked gays, whipped up a climate of racism--the list could go on all day--sees itself as "moderate"! It wants to convince us that it is in the mainstream by claiming that it is fighting "extremism."<p>
Just as we exposed the lying basis of the "war on terrorism," we need to expose this shift in language for what it is--just another attempt by Bush and Co. to increase the wealth and power of the rich at the expense of working people here and around the world.<br>
<b>Steve Leigh,</b> Seattle<p>

