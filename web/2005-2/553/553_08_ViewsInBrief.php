<p><a name="Wrong"></a><font face="Times New Roman, Times, serif" size="4"><b>Wrong to split antiwar forces</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ATROCITIES LIKE the invasion and occupation of Iraq and the occupation of Palestine are not going to end as long as the system of production for profit that gives rise to them persists. Lance Selfa knows this, as do most <i>SW</i> readers ("Unity on what basis?" August 5). And yet, we do not insist that every antiwar rally demand the end of capitalism.<p>
Socialists participate in movements for partial, short-term reforms for a variety of reasons, not least that we want to achieve those reforms, like ending the U.S. occupation of Iraq, in the here and now. Another reason is that we know from decades of experience that we can win people to the socialist project more effectively while standing shoulder to shoulder with them in a common struggle than in any other context. <p>
The statistics Lance cites appear to show that opposition to the occupation is much broader than the organized left. Indeed, among those who oppose the war on Iraq, many continue to harbor illusions in Zionism. Lance is absolutely right to point out the inconsistency of this position. <p>
But when it comes to Zionism, otherwise apparently intelligent, rational people completely lose the plot. While asserting that they oppose terrorism and ethnic cleansing they are comfortable implicitly accepting the terrorism of the Stern Gang, as well as of the Israeli state and the ethnic cleansing of 1948-49. They support democracy and oppose sectarian states, except in Israel.<p>
The real basis of the argument for watering down or omitting demands for Palestinian rights in the antiwar movement is this: We can separate more people from their Zionist illusions when we are fighting the U.S. occupation of Iraq together than when we insist, without making the arguments in a comradely atmosphere, that they adopt anti-Zionist demands like the right of return. However, as Lance presents the situation, there is an additional factor to take into consideration. <p>
The question has now become whether we should we drop an important and relevant demand to create an opportunity to intersect with Zionists, or demand the right of return, which will empower and include Arabs and Muslims, who are themselves very direct victims of Bush's war of terror. Under the circumstances, we call for unity, but if the Zionists want to split the movement, let it be on their heads.<br>
<b>Anonymous,</b> From the Internet<p>

