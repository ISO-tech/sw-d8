<p><a name="Left"></a><font face="Times New Roman, Times, serif" size="4"><b>Left is rejecting the Greens</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ASHLEY SMITH writes: "Thus, in an answer to a question after his speech, Cobb denounced <i>CounterPunch's</i> Alexander Cockburn, saying that he 'represents why the sectarian left has failed.' The not-so-subtle message was that the Green Party should exclude the left and continue cooperation with liberal Democrats" ("Showdown in the Green Party," August 5).<p>
I'm the one that asked Cobb the question, which was how he felt about being the object of Alexander Cockburn's ridicule and scorn in an article in CounterPunch denouncing his candidacy in 2004. And I think Smith has it upside down. The Greens haven't rejected the left. Cockburn has rejected the Greens. <p>
After John Kerry's betrayal of progressives last year, I for one do not advocate continued cooperation with liberal Democrats. If I did, I would have joined the Progressive Democrats of America instead of the Green Party earlier this year, and I know the majority of Greens at the Tulsa Convention agree with me.<br>
<b>Audrey Clement</b>, from the Internet<p>

