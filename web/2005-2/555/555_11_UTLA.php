<font face="Times New Roman, Times, serif" size="4">Union reformers target Schwarzenegger's attacks</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>UTLA leaders vow to fight</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs, United Teachers Los Angeles</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 2, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">THE NEW leadership of United Teachers Los Angeles (UTLA) presided over the union's Leadership Conference and outlined a plan to fight cutbacks and Gov. Arnold Schwarzenegger's anti-union ballot measures set for the November election.<p>
One of Schwarzenegger's proposals would restrict the use of union funds on political issues. Another takes aim directly at teachers' unions by extending teachers' probationary period from two years to five and making it easier to fire any teacher who gets poor performance reviews two years in a row.<p>
The UTLA Leadership Conference--the first under new UTLA President A. J. Duffy--enabled teachers to strategize over these and other pressing issues.<p>
Duffy defeated incumbent President John Perez in last spring's union elections in a campaign that skewered Perez's do-nothing leadership and pledged to mobilize UTLA members to fight the attacks on public education. Duffy had the endorsement of the United Action reform slate, whose candidates won three officer positions and a majority of seats on UTLA's Board of Directors.<p>
These new leaders have their work cut out for them. From the federal No Child Left Behind (NCLB) law to Schwarzenegger's budget cuts and the bloated bureaucracy of the Los Angeles Unified School District (LAUSD), UTLA members face attacks on all fronts.<p>
To take on this challenge, members' confidence in our union needs to be rebuilt. After UTLA members voted in Duffy and United Action, John Perez came back to UTLA members with a lousy contract offer from LAUSD.<p>
United Action, Duffy, and UTLA's House of Representatives all called on members to reject the contract--which included a salary freeze in 2003, a 2 percent raise in 2004, and no relief from huge class sizes. But UTLA members narrowly approved the deal when 54 percent of those voting backed it.<p>
The approval of the contract highlighted the ways in which the attacks on teachers--combined with the inadequate response from union leaders--have lowered members' expectations over many years.<p>
The good news is that the new UTLA leadership used this year's Leadership Conference to try to reverse this losing dynamic. In his speech at the conference Duffy said, "We cannot afford the luxury of relying on politicians.  They have their own agenda.  We need to mobilize and organize and build alliances with parents and our communities." Duffy also announced this year's contract re-opener demands: full maintenance of health benefits, a 5 percent raise, class size caps, a reduction of district-mandated tests, and a 7.5 percent cut in LAUSD bureaucracy.<p>
Past UTLA President Wayne Johnson, beloved by veteran teachers for his leadership of UTLA's successful 1989 strike, brought the house down with a blistering speech. He roundly condemned standardized testing and compared the choice between Democrats and Republicans to "paper or plastic."<p>
Along with this militant talk came a welcoming atmosphere towards internal debate and dissent in UTLA. Progressive Educators for Action (PEAC)--a rank-and-file caucus that has revitalized union chapters at several LAUSD schools and was instrumental in electing the new leadership--had a prominent role at this year's conference.<p>
UTLA is launching a massive effort to mobilize members in a campaign against Governor Schwarzenegger's union-busting November ballot measures.<p>
Defeating these attacks and winning our contract demands will mean rebuilding activism among UTLA members. Given the union's recent long state of torpor, that's a big job. But the election of a new leadership in UTLA has opened the door to a new set of possibilities.<p>

