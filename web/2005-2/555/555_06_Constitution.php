<font face="Times New Roman, Times, serif" size="5"><b>Collapse of the constitution charade</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 2, 2005 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">THE LATEST of the Bush administration's much-hyped "milestones" for the "new Iraq" collapsed into quarreling and chaos in August.<p>
After nearly two weeks of delays beyond an August 15 deadline, leaders of the new Iraqi government submitted a final draft of a constitution to be voted on in an October referendum. But the proposal was rejected by representatives of the country's Sunni Muslim minority--and is in direct violation of conditions and rules imposed on the whole process under the U.S.-concocted Transitional Administrative Law.<p>
Though George Bush nevertheless hailed the draft as "an inspiration to all who share the universal values of freedom, democracy and the rule of law," the constitution meltdown is a blow to the administration. U.S. officials had expected their allies in the new Iraqi government to deliver a constitution that Sunni, Shiite and Kurdish leaders all endorsed, in spite of their conflicts.<p>
Instead, leaders of Iraq's Shiite Muslims, who make up about 60 percent of the population, and of the Kurdish minority with its base in the north satisfied their own interests, but refused to meet the demands of Sunnis, who are about a quarter of the population. Bush personally intervened to urge Shiite powerbrokers to make concessions, but the minor changes this produced weren't enough to win over Sunni negotiators.<p>
First, the constitution bans the Baath Party--the former ruling party of Saddam Hussein's Sunni-dominated regime--"and its symbols, under any name." This was a prime objective for leaders of the Shiites, who were repressed under the Hussein government, but Sunni representatives say the provision will exclude them from political life.<p>
The second objection is to provisions that allow individual provinces to unite and form regional governments--the mechanism for potential mini-states to arise within Iraq.<p>
The Kurds, with the backing of the U.S. since the 1991 Gulf War, already have regional autonomy and plan to extend their control further, particularly in the country's northern oilfields. Leaders of the main Shiite parties in the new government want to develop something similar in the south--giving them control over the vast oil wealth in southern Iraq. The Sunnis would be cut out of power in this shift toward "federated regions."<p>
But opposition to the constitution isn't solely based on religious and ethnic divisions. Among Shiites, there are sharpening conflicts that were thrown into sharp relief amid the constitution meltdown by armed battles between the Badr militia loyal to the main Shiite party dominating the government, the Supreme Council for Islamic Revolution in Iraq (SCIRI), and the Mahdi Army that supports militant Shiite cleric Moktada al-Sadr.<p>
Al-Sadr's base is among the Shiite poor who live in the slums of Iraqi cities, particularly in the neighborhoods of Baghdad. He has been largely left out of the political horse-trading among Shiite leaders, especially the plans to establish Shiite power through a southern mini-state.<p>
Al-Sadr called on his supporters to stop the fighting last week. But this conflict is likely to escalate, with al-Sadr threatening to call for a "no" vote on the new constitution in opposition to its move toward "federalism."<p>
Together, Sunni Muslims and al-Sadr's followers could defeat the constitution in the referendum. A two-thirds "no" vote in three of Iraq's 18 provinces would scuttle the deal. But the other forces involved have a lot at stake in the new constitution, and their control of the government in the run-up to the referendum could produce a rigged vote.<p>
Whatever the outcome, the Bush administration's occupation of Iraq has suffered another setback. Rather than a step closer to stability and reconstruction, the August constitution breakdown will only produce more chaos and violence--in a society where the vast majority have already seen their conditions grow steadily worse since they were "liberated."<p>

