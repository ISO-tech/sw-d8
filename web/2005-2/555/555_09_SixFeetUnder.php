<font face="Times New Roman, Times, serif" size="4">Taking on social taboos--at a funeral home</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why <i>Six Feet Under </i>was so popular</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by David Thurston</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 2, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><i>Six Feet Under, </i>starring Peter Krause, Michael C. Hall and Frances Conroy. Available on DVD.</font><p>

<font face="Times New Roman, Times, serif" size="3"><i>SIX FEET Under</i> ended its fifth and final season on August 21. The HBO series, created by Alan Ball, the screenwriter of the 1999 movie <i>American Beauty</i>, is built around the Fisher family and its Los Angeles funeral home.<p>
Few would have thought that a show so centered on death and grief could become so popular. <i>Six Feet Under</i> has grappled with the hypocrisy of conventional morality, the pain and conflict of family life, and both the joy and brutality of sexual relationships.<p>
The show's characters are often described as "dysfunctional." But the pain that the show's characters confront are more common than we're taught to admit.<p>
Issues of mental health and substance abuse are faced with a level of humanity that is sadly absent not only from popular culture but in society as a whole. The show confronts these taboos and conveys an understanding of choices and behaviors that are usually ignored or demonized.<p>
One of the show's key contributions has been the way it has addressed issues of oppression. Over the first two seasons, we watched David (Michael C. Hall) struggle to come out to his family and deal with his shame and guilt about being gay.<p>
David and his partner Keith (Matthew St. Patrick) are developed as people, not stereotypes. This might be the best portrait of gay life on TV, in part because these two characters are integrated into a wider cast without the pretense of being iconically representative.<p>
<i>Six Feet Under</i> gave us a striking range of compelling female characters. Ruth Fisher, played by Frances Conroy, is a mother of three and the picture of maternal constriction and repressed emotion. Married since she was 19, she grapples with regret at sacrificing broader life goals to marriage and family.<p>
Given the pliant stereotypes that dominate the airwaves, it's refreshing to see women actively challenging sexism coming from men in their lives. This is true of Ruth's daughter Claire and of Brenda, who is eldest son Nate's (Peter Krause) lover and partner for much of the show.<p>
Brenda's struggle with an abusive childhood and destructive emotional patterns is given admirable depth through the show's five seasons. The show also stands out in having a leading Latino character, Federico Diaz (Freddy Rodriguez), who challenges some of the Fishers' cultural blinders.<p>
The show's final season has been increasingly political, peppered with tirades against Bush and the war in Iraq. In the finale, Claire begs her conservative boyfriend to go to Canada if "the corporate warmongers invade Iran and bring back the draft." A week before, we watched a veteran of the current war who has lost three limbs commit suicide with the help of his sister.<p>
As a show about a funeral home, a constant theme has been its examination of how we deal with death and our mortality. As creator Alan Ball put it recently, "In this culture we just sort of grieve quietly. You don't want to embarrass people with the big emotions."<i> Six Feet Under</i> has been the antidote to all of the repressive implications of these unspoken social rules.<p>
The stunning final sequence is a montage of scenes from the characters' futures, set against Claire leaving Los Angeles for New York to pursue photography. The show projects the deaths of each of its main characters. Yet, as the sequence develops, we watch Claire grow confident about moving forward.<p>
The message here has been a running theme--that by accepting the reality of death and grief, we can live our lives more fully in a world so full of hardship.<p>
<i>Six Feet Under's</i> intelligence, humor and honesty will be missed. Fortunately, the show's first three seasons have been released on DVD, with the rest to follow.<p>

