<font face="Times New Roman, Times, serif" size="5"><b>Chicago clergy challenge police racism</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Keeanga-Yamahtta Taylor</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 2, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--More than 350 people participated in a town hall meeting August 22 on the issue of racial profiling. There has been a spotlight on the issue of racial profiling since the Rev. James Meeks, who is also a Democratic state senator, was pulled over and threatened with a gun by Chicago cops in early August.<p>
The meeting was organized by a South Side church and included more than a dozen pastors from other South and West Side churches and prominent Black attorneys, and coincided with two-hour programs on racial profiling on two Black radio stations in the city.  <p>
The meeting highlighted the cases of several African Americans and Latinos who have been victimized by police brutality and racial profiling.<p>
There were two Black teenage boys who were arrested in front of one of their homes while playing with a basketball. Witnesses to the robbery of a freight train, they were arrested and booked for the crimes themselves.<p>
Another example cited was that of a 42-year-old Black man, an independent construction contractor, who was pulled over and repeatedly called "boy" by white officers. He was pulled over because his licenses plates were improperly registered--and was eventually arrested and jailed for disorderly conduct when he questioned why the police were asking to search his car.<p>
And then there was the Black female police officer who lives in the Cabrini-Green neighborhood, and who attempted to intervene when the police were harassing other people near her home. She was beaten and threatened with arrest by the police until they figured out she was also a cop.<p>
The pastors who organized the meeting say they want videotapes in all squad cars as a way to monitor the behavior of police during traffic stops.<p>
While there are similar meetings scheduled for the West and North Sides of Chicago, there are no plans for any marches or demonstrations against racial profiling and police brutality. This is unfortunate, because these religious leaders are in a position to mobilize thousands of ordinary Blacks in the city to protest the police.<p>
Another problem was the apparent lack of invitation extended to Muslims and Arabs to participate, although Latino and white clergy were present. To be effective, organizing against racial profiling must extend solidarity to Muslims and Arabs, who are increasingly targeted by law enforcement as part of the "war on terror."<p>
In fact, the Department of Justice just released statistics showing that racial profiling of minority motorists is on the rise. We need a new movement against racial profiling in this country that cuts across all racial lines and unites everyone who is victimized by racist police departments across the U.S.<p>

