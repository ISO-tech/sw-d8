<font face="Times New Roman, Times, serif" size="5"><b>An altogether rotten human being</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 2, 2005 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">CHRISTOPHER HITCHENS is more than a pathetic, soused ex-leftist with an almost pathological need to press his lips against the buttocks of American power. He is an altogether rotten human being. <p>
His diseased soul was on clear display in a recent column titled "Cindy Sheehan's Sinister Piffle." After calling Sheehan's vigil in Crawford "dreary sentimental nonsense," he casually--like an infant flinging their poop at the wall--states that Cindy is an anti-Semite who has "David Duke flying to her side." <p>
He then flies off onto a garbled flight of repugnant fancy, imagining himself in Cindy's shoes: "Suppose I had lost a child in this war. Would any of my critics say that this gave me any extra authority? Why, then, should anyone grant them such a privilege?"<p>
We cannot write off Hitchens' attack as just another case of too much scotch. Hitchens is reflecting the hysteria in the pro-war camp that Sheehan may provide a focal point for the antiwar silent majority in the United States. Therefore, slandering a woman who lost her 24-year-old son in a war of their making becomes a question of the utmost urgency, and in the process reveals their collective moral bankruptcy. <p>
As for Hitchens, I for one cannot wait until later this month, when he debates British Member of Parliament George Galloway in New York City. I think Hitch will find that an audience of New Yorkers will be a touch less kind than the D.C. politicos that pat his head, scratch his ears and snicker behind his back, as he wobbles to the next cocktail party on Embassy Row. <br>
<b>Dave Zirin, </b>Washington, D.C.<p>

