<p><a name="Swedish"></a><font face="Times New Roman, Times, serif" size="4"><b>Swedish Medical Center</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Kate Johnson</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--Workers at Swedish Medical Center, members of SEIU 1199NW, held informational pickets and rallies on August 16 to build solidarity for their bitter contract fight with management. About 200 workers, union members and labor activists turned out for the rally and march around the medical center in the First Hill neighborhood, the hospital's biggest center.<p>
Despite almost getting shut down hours earlier by management, who begged the courts to intervene and order the demonstration illegal, workers were in an optimistic and fighting mood. Armed with bullhorns, chants and lots of picket signs, they marched around the hospital chanting "Patients not profits!" before they held a rally that featured speakers, musicians and politicians.<p>
In July, SEIU employees at Swedish rejected management's initial offer by a 98 percent margin, sending the contract back to the negotiating table.<p>
At the heart of the battle is management's proposal to slash quality, affordable health care and scrap pension plans for new workers, creating a permanent two-tier system. Current workers would be able to keep their pension plans, but new hires would be forced into a less-secure 401(k) plan instead--a classic divide-and-conquer strategy by the bosses.<p>
But the workers are determined to keep this proposal out of their contract. As one nurse told <i>Socialist Worker, </i>"It [Swedish's proposal] is part of the war on working people. We all deserve quality health care. We all deserve a pension plan!"<p>
Swedish is the largest hospital in Washington--and is one of the most profitable. In the past three years, Swedish has made $158 million in pure profit. But instead of raising standards for health care jobs, Swedish is demanding unreasonable concessions from its frontline workers to line its greedy pockets.<p>
Swedish workers are determined to keep up the fight for a good contract, and the recent pickets are a great first step in building much-needed solidarity and community support. "Sure," one nurse said, "we'd rather be at the bargaining table right now, but they have forced us out on the streets."<p>
And taking it to the streets is exactly what they must continue to do! Solidarity, rank-and-file involvement and preparation for a militant fight will be key to winning a good contract that secures quality standards of living for Swedish Medical Center workers.<p>

