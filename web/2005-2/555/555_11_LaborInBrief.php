<p><a name="Boston"></a><font face="Times New Roman, Times, serif" size="4"><b>Boston security guards</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By John Moore, Museum Independent Security Union</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BOSTON--Security guards at Boston's Museum of Fine Arts, members of the Museum Independent Security Union, have been working without a contract since March 18. <p>
Guards are paid between $11 and $13 an hour, and we have seen a 20 percent reduction in full-time jobs. The Museum has embarked on a $525 million building-expansion campaign, and yet what they have out on the negotiating table now is a net loss in wages in one of the most expensive cities in the country.<p>
A part of this equation is that they are taking away time-and-a-half pay after eight hours. Currently, three hours and 36 minutes of overtime are a part of our regular 40-hour workweek. In order to make our 40 hours a week, we are expected to work from 9:30 a.m. until 10:15 p.m. on Wednesdays. Thus, the 2 percent raise that they are offering us is a net loss.<p>
They want to take protective language concerning our pension plan out of our contract. They also want to take away the 320 hours of accrued sick leave buyback that we now can take with us when we resign or retire.<p>
They are continuing the slaughter of making full-timers part-time, and they have cut 21 full-time positions from the galleries in the last decade. Currently, they pay the employees who opt out of the museum's health plan $500 a year. They are also taking that away.<p>
We are asking everyone to e-mail or call the director of the Museum of Fine Arts of Boston, and demand that they work out a fair contract for the guards in our union. <p>
<i>Protest the poor treatment of the Museum Independent Security Union by calling 617-369-3200 or e-mailing <a href="mailto:mrogers@mfa.org">mrogers@mfa.org</a></i>.<p>

