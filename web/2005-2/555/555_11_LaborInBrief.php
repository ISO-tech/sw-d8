<font face="Times New Roman, Times, serif" size="4"><b>Space Needle restaurant</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>Ellie Fingerman, UNITE HERE Local 8</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--After stalling negotiations, then proposing negative pay raises and insulting work rules to its employees, the Space Needle is taking an even harder line.<p>
During the last so-called negotiating session, the employers sat in the room for approximately 40 minutes out of the five hours scheduled for talks. Now they've sent out a nasty letter with employee's paychecks, trying to paint the union as unconcerned about Space Needle workers and more concerned with "industry standards."<p>
Fortunately, the union is stepping up the pressure as well. On August 18, more than 60 people came out to a rally organized by UNITE HERE Local 8 right outside the Needle. Community members, union brothers and sisters and several local politicians turned out to show their support for the workers and their frustration with Space Needle management. Many speakers mentioned that the Space Needle is a symbol of Seattle, and that it should symbolize fairness and workers' rights, not corporate greed.<p>
The union expects the Space Needle's hired lawyer to try to bust the union by sending out more letters to the membership and talking to workers about decertification. More steps are being taken, such as house visits and a petition to the chairman of the board, to continue to build the campaign for a good, fair contract.<p>
In a related incident aimed at intimidating union members, a hard-working and dedicated employee was recently fired after being delayed by the elevator and clocking in two minutes late. A petition to get his job back was immediately signed by more than130 employees, and a grievance is proceeding against the company. If the union is successful in getting this person re-hired, it will give a huge boost of confidence to union members to fight back.<p>

