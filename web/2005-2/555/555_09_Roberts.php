<font face="Times New Roman, Times, serif" size="5"><b>Why they aren't fighting Roberts</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 2, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">EVERY TIME a left-wing third-party challenge emerges in a presidential election, liberals and Democrats pull out their trump card. "What about the Supreme Court?" they scream. "Democrat X may not be good enough for you, but at least he won't appoint another Scalia to the Supreme Court."<p>
And when they really get apoplectic, they rant, "You may not care about the right to abortion, or affirmative action, but who's on the Supreme Count makes a difference to millions of women and minorities."<p>
Those of us who have had to endure these lectures wonder just who is looking out for affirmative action and abortion rights today? The liberals don't seem to be.<p>
Bush has nominated John Roberts, who, with each revelation of his Reagan-era positions on such issues as comparable worth pay for women and AIDS, demonstrates that he's Scalia with a smile. Yet the liberal groups have mounted a quiet and inept campaign against him, punctuated by NARAL Pro Choice America's withdrawal of a TV ad linking Roberts to the anti-abortion zealots in Operation Rescue.<p>
For years, the liberal groups have been collecting millions with fundraising appeals raising the specter of a right-wing dominated Court. Now that the time has come to do something with those millions, the liberal groups appear to be flummoxed.<p>
"If we are asking the Senate to be careful and deliberate, then we have to, in the same way, take the same stance," Karen Pearl, interim president of Planned Parenthood of America, told <i>Salon</i> in July. So much for fighting words.<p>
Part of this reticence comes with these groups' Beltway mindset. Rather than wage an ideological battle or attempt to rally forces around the country, they're sifting through Roberts' papers hoping to find some "smoking gun" that they can cripple his nomination. Perhaps they hope to find evidence of some peripheral issue--like smoking dope in college or hiring an undocumented immigrant.<p>
But mostly it comes from the fact that leading Democratic senators have indicated that they don't plan to put up much of a fight against Roberts. If the liberal groups have based their entire strategy on winning the Democrats to filibuster "extreme" nominees, then they're left with nothing when Democrats decide not to go along.<p>
During the September Senate hearings on Roberts, we'll likely have to put up with hours of evasive testimony and Democratic questioning that dances around the crucial issues--all before Roberts' nomination sails through the Senate.<p>
This is yet another example of how the liberal groups that conservatives always accuse of dictating policy to the Democrats are instead captives of the Washington establishment. They aren't so much independent representatives of women, Blacks and other oppressed groups as they are Democratic Party satellites that "hold the portfolios" for women, Blacks and other oppressed groups.<p>
They try to stay "on message" with Democrats while the Democrats run away from defending abortion rights or affirmative action. And while they have money and (supposedly) influence in Washington, they rarely organize people to fight on their own behalf. Instead, they're adept at relating to struggles that emerge with the idea of milking them for electoral advantage.<p>
A recent example was MoveOn.org's call for August vigils to support Cindy Sheehan, the antiwar activist whose son was killed in Iraq and who captured world news attention with her protest at Bush's summer retreat.<p>
Although the vigils were very successful in giving thousands of people a way to express their opposition to the war, MoveOn remains unchanged in its support of the continued occupation of Iraq. MoveOn had made a fairly public decision to drop the war as one of its focuses earlier this year.<p>
But when Sheehan succeeded in galvanizing opposition to Bush, MoveOn and its money swooped in to Crawford. Nevertheless, MoveOn has no intention of championing Sheehan's demand for immediate withdrawal from Iraq.<p>
While grassroots antiwar activists try to rebuild the antiwar movement, you can bet that the likes of MoveOn will "move on" to another issue when Sheehan isn't making headlines anymore.<p>

