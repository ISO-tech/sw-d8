<font face="Times New Roman, Times, serif" size="5"><b>Killed by guards at a Wal-Mart store</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Cindy Beringer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 26, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">A 30-year-old man was killed in the parking lot of a Wal-Mart by store employees who suspected him of shoplifting. Stacy Clay Driver was chased into the parking lot by five "loss prevention employees" who tore off Driver's shirt and wrestled him bare-skinned onto the blistering asphalt. <p>
Charles Portz, a prominent Houston attorney who was getting out of his car when the incident occurred, gave the gruesome details to local media. According to Portz, one Wal-Mart guard had Driver in a chokehold with a knee in his back. "[Driver] kept trying to get up, and they kept pushing him back down," Portz said.<p>
Driver begged the guards to let him up and to call for an ambulance, saying that he was going to die. The Wal-Mart employees called police several times, but it was left to one of 30 witnesses to the brutality to call for medical help.<p>
The bystanders begged the torturers to let Driver up. Portz said he pointed out that Driver's fingernails were turning gray, but "they said he was just high on something," he said. When paramedics arrived, they found Driver--handcuffed behind his back and lying face down--had stopped breathing. He was pronounced dead at the hospital.<p>
The <i>Houston Chronicle </i>reported that Driver was suspected of shoplifting a package of diapers, sunglasses and several other inexpensive items. Employees later said that he had changed price stickers. Portz said that Driver had only a pair of sunglasses in his possession.<p>
Sgt. Jeff Stauber of the Harris County Sheriff's Department said that Wal-Mart is known for its aggressive pursuit of shoplifters. Three years ago, Wal-Mart security personnel chased a suspected shoplifter onto a major freeway in Houston, where he was killed instantly by an 18 wheeler. Wal-Mart has faced several civil lawsuits for false imprisonment and malicious prosecution, Stauber added.<p>
At the time of Driver's murder, the media were devoting countless hours to the "BTK" killer in Kansas, and gave no attention to the killing in a Wal-Mart parking lot. Wal-Mart refused to comment, and no charges were filed.<p>
This corporate giant has been getting away with murder on many levels for decades--reminding us of the difference between their morals and ours. <p>


