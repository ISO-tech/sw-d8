<font face="Times New Roman, Times, serif" size="5"><b>A show of solidarity with Venezuela</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 26, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>BOB QUELLOS, BEL&Eacute;N CADENA and SARAH HINES report from the World Youth Festival in Caracas, Venezuela.</b><p>

THIS YEAR'S World Youth Festival (WYF) was a massive display of solidarity with the people of Venezuela and their struggle against U.S. imperialism. More than 15,000 young people from 140-plus countries came together in Caracas for the week of political seminars and cultural activities.<p>
This is the 16th WYF held since 1947. Organized by Communist Parties internationally, the festivals have been organized in different countries around the world, under varying political slogans, including opposition to fascism and solidarity with Cuba. <p>
Having Venezuela as the site for this year's festival had a galvanizing effect. Over the last several years, the people of Venezuela have defeated three U.S.-backed attempts by the country's right wing to unseat President Hugo Ch&aacute;vez. These victories have created a highly politicized poor and working class mobilized against the threat of U.S. imperialism and neoliberal economic policies.<p>
Ch&aacute;vez has continued to respond to the movement by calling for "21st-century socialism in Venezuela." This has sparked an open debate within the country about what socialism will look like.<p>
The discussion was at the front of everyone's mind at the WYF. "It's not enough just to wear a red shirt," said Katiusca Garcia Urbaneja, a high school chemistry teacher in Anzoategui. "The problem that we have now is to raise the consciousness of the people about the meaning of socialism, communism, oppression and imperialism...In defense of Ch&aacute;vez, the whole population is clear. But this isn't enough."<p>
Unfortunately, the political seminars and workshops at the WYF failed to take full advantage of the opportunities for debate. Most of the political panel discussions were put together under large umbrella topics that allowed activists to discuss accomplishments in their countries without looking any further ahead. This vacuum was often filled by calls for solidarity with the Bolivarian Revolution and Cuba--which unfortunately displaced needed critical debate and depoliticized many parts of the conference.<p>
There were other issues missing from the WYF. Representatives from Venezuela's barrios--where many of the greatest social changes are taking place--didn't have a platform at the conference, leading one local activist to comment, "The revolution wasn't invited."<p>
But if the official agenda was limited, the atmosphere surrounding the WYF was alive with discussion. The U.S. delegation to the festival numbered 840 people strong--representing many organized left groups alongside hundreds of independent activists, which created a great openness to taking up the question raised in Venezuela.<p>
By bringing together 15,000 young activists, the festival's greatest accomplishment was to strengthen the ties of youth organizations around the world and within countries. Connections made at the WYF can help in coordinating international actions and advancing political discussions about our ideas and vision of a socialist future.<p>
Plus, the WYF gave thousands of young people firsthand experience in the exciting developments of the movement in Venezuela--its debates, its missions and the obstacles it faces. This in turn raised many people's political confidence and expectations--and brought the question of socialism from the abstract into the present. <p>

