<font face="Times New Roman, Times, serif" size="4">Papers reveal Supreme Court nominee's contempt for women's rights</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Could John Roberts be any more right wing?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 26, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>NICOLE COLSON reports on the revelations about John Roberts' right-wing record.</b><p>

<font face="Times New Roman, Times, serif" size="3">THE RECORD of Judge John Roberts--the Bush administration's pick to replace Justice Sandra Day O'Connor on the Supreme Court--isn't as conservative as progressives initially thought. It's worse.<p>
Every few days has brought new revelations of his right-wing positions, from his opposition to abortion rights and affirmative action, to his upholding the arrest and of a 12-year-old African American girl for eating a single French fry on the Washington, D.C., Metro.<p>
Most recently, <i>USA Today </i>found that, as an assistant White House counsel, Roberts mocked the idea that women should receive equal pay. According to the paper, in 1984, Roberts wrote a memo responding to three female Republican members of Congress--including now-Sen. Olympia Snowe (R-Maine). The three encouraged the Reagan administration to let stand a court ruling (later overturned by a higher court) that federal anti-discrimination law required equal pay for men and women who held different jobs requiring comparable skills and effort.<p>
Roberts' response nearly accused the three women of being communists. "I honestly find it troubling that three Republican representatives are so quick to embrace such a radical redistributive concept," he wrote. "Their slogan may as well be, 'From each according to his ability, to each according to her gender.'"<p>
In a separate review of a report that summarized state efforts to combat discrimination against women, Roberts also referred to a "staggeringly pernicious law codifying the anti-capitalist notion of 'comparable worth,' (as opposed to market value) pay scales." <p>
Roberts' disdain for women's rights didn't end with the idea that they should get equal pay. In a memo regarding the nomination of an administration aide to a scholarship program to honor women who made changes in their lives after age 30, Roberts signed off on the nomination, but added: "Some might question whether encouraging homemakers to become lawyers contributes to the common good, but I suppose that is for the judges to decide." <p>
Other recently released documents show that Roberts counseled the Reagan administration against saying that AIDS couldn't be transmitted through casual contact--and that he advocated a national ID card to stop "the real threat to our social fabric posed by uncontrolled immigration." In anticipation of a presidential interview with <i>Spanish Today, </i>Roberts wrote, "I think this audience would be pleased that we are trying to grant legal status to their illegal amigos."<p>
These revelations are probably the tip of the iceberg. The White House is refusing to release thousands more pages of records relating to Roberts' time as deputy U.S. solicitor general under the administration of George Bush Sr.--during which time Roberts weighed in on important cases involving abortion rights, school desegregation and religion in public places. <p>
Yet despite Roberts' rotten record, there's little indication that the Democrats will put up a serious fight to oppose his confirmation. As Senate Minority Leader Harry Reid (D-Nev.) said in a statement last week: "All this talk about whether Democrats will support the Roberts nomination is laughably premature."<p>

