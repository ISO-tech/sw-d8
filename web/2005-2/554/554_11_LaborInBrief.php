<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">August 26, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Caldwell Manufacturing</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Brian Lenzo</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ROCHESTER, N.Y.--Thirty-eight members of IUE-CWA Local 81331 are on strike to defend their union shop at Caldwell Manufacturing, a third-generation family-owned maker of window hardware. Workers have been working under a contract that the company illegally imposed since April 26, 2004.<p>
Management is insisting on a wage freeze, higher health care co-pays and no increase in retirement benefits, claiming they need to stay "competitive." But as one striker explained, "Caldwell has been a closed shop since the 1950s when the union was formed. What they really want is to break our union."<p>
"The company is greedy," explained union activist Bill Carson. "They want to bring in low-paying jobs with no benefits. It will become a sweatshop."<p>
Workers went out following rulings by the National Labor Relations Board that qualified the strike as an "unfair labor practice dispute," thus legally preventing Caldwell from employing "permanent replacement workers." However, temporary scabs are okay, and Caldwell is bringing them in.<p>
Local labor is beginning to rally around the struggle, which strikers expect to be a long one. This strike also represents something bigger, in a city that is hemorrhaging jobs. Rochester fell from 129,000 manufacturing jobs in 1990 to 45,000 in 2005. Since 2003 alone, Rochester has lost 10,000 jobs. <p>
Solidarity from labor activists and from other local unions, together with loud and aggressive pickets, will be crucial to stopping Caldwell from doing business as usual. The local has called a rally for 2:30 p.m., August 25, in the entrance of the plant, to send a message to management.<p>
"Our local has drawn the line," said one striker. "We're a small union, but we've got a lot of fight in us."<p>
<i>Send messages of support or donations to IUE-CWA Local 81331 Strike Fund, P.O. Box 246, Rochester, NY 14514-9998</i>.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Madison Market Co-op</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Darrin Hoop, UFCW Local 1105</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--Workers at Madison Market Co-op voted down management's contract offer, by a 27-19 margin, on August 17. The 60 members of United Food and Commercial Workers (UFCW) Local 1105 have been working under the terms of a contract that expired on August 1, 2004.<p>
The proposed contract contains many gains, including a ban on drug testing, the elimination of the lower tier/lower paid courtesy and helper clerks and the practice of moving them into the higher-paid grocery classification, and paid paternity leave. But the key issues that led to the "no" vote were management's offers of just 30 cents increase for workers who've worked over three and a half years at the store and retroactive pay back to just June 1, 2005.<p>
The rejection of the contract follows the first vote on the contract August 4, which resulted in a 22-22 tie vote. Since then, workers have not only had to deal with pressure from management, but union officials have been telling workers to vote "yes" behind the negotiating team's back. They also lied to the staff, claiming that if they voted "no," it meant we were going on strike. <p>
To counter management's and union officials' misinformation, a couple of workers put out informational flyers that helped cut through the lies and talked to coworkers about where money for wages for everyone could be found. For example, the recently resigned store manager's salary could easily pay for a 30 cents raise for the entire staff for a year, since his position hasn't been filled. <p>
We posted flyers all over the store notifying staff of the vote, and used a phone list to call throughout the day of the vote to make sure workers voted. We even had a van available to pick up any workers who needed transportation to the store to vote. With more than 75 percent of the eligible staff voting, the high turnout was key to the rejection of the contract.<p>
As <i>Socialist Worker </i>went to press, the union was waiting to hear from management's corporate lawyer, who is currently on vacation, about when the two sides will return to the negotiating table. The "no" vote showed that the workers won't be happy until there's equal pay increases for everyone.<p>

