<font face="Times New Roman, Times, serif" size="4">Antiwar MP announces U.S. speaking tour</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Mr. Galloway goes back to Washington</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 26, 2005 | Page 5</font><p>

<font face="Times New Roman, Times, serif" size="3">THE PEOPLE who run the "war on terror" have learned to hate George Galloway. A leader of the British antiwar movement and now a member of parliament, Galloway has made himself a constant thorn in the side of those who invade and bomb the innocent, trample on civil liberties and bow down to corporate interests.<p>
This September, he'll put the U.S.-British war against Iraq on trial from this side of the Atlantic--during the recently announced "Stand Up and Be Counted" speaking tour to nine North American cities.<p>
Galloway was expelled from British Prime Minister Tony Blair's Labour Party in 2003 because of his outspoken opposition to the invasion of Iraq. In a television interview, Galloway questioned the legality of the war and accused both Blair and George W. Bush of lying to soldiers about how long the war would last. "They have lied to the British Air Force and Navy when they said the battle of Iraq would be very quick and easy," he said.<p>
Galloway was right, of course, but his statements--including urging British troops to disobey illegal orders--got him kicked out of Blair's "New Labour." But earlier this year, Galloway won back a seat in parliament by a large margin--this time, running as a candidate of the left-wing Respect coalition.<p>
Right after that, Mr. Galloway went to Washington. In May, after a Senate subcommittee released a report recycling discredited allegations that a campaign Galloway was involved in had been paid off by Saddam Hussein's regime in exchange for opposing the brutal regime of U.S.-backed economic sanctions, Galloway challenged Washington to let him clear his name.<p>
Taking advantage of an invitation to appear before a Senate committee, Galloway singled out chair Sen. Norm Coleman (R-Minn.) in his opening statement. "Senator, in everything I said about Iraq, I turned out to be right, and you turned out to be wrong," Galloway said. "And 100,000 people paid with their lives--1,600 of them American soldiers sent to their deaths on a pack of lies; 15,000 of them wounded, many of them disabled forever on a pack of lies...[I]f the world had listened to me and the antiwar movement in Britain, we would not be in the disaster that we are in today. Senator, this is the mother of all smokescreens."<p>
After taking his seat in parliament, Galloway made the case following the July bombings in London that the "war on terror" has made the world less safe, not more so.<p>
"The loss of innocent lives, whether in this country or Iraq, is precisely the result of a world that has become a less safe and peaceful place in recent years," he said in a statement. "We argued, as did the security services in this country, that the attacks on Afghanistan and Iraq would increase the threat of terrorist attack in Britain. Tragically, Londoners have now paid the price of the government ignoring such warnings."<p>
This September, Galloway will return to the U.S. for a week and a half of speaking engagements, leading up to the September 24 national antiwar mobilization in Washington, D.C. The tour--titled "Stand Up and Be Counted: No to War and Occupation"--is sponsored by the New Press, which is publishing a new book by Galloway; the <i>International Socialist Review; </i>the Center for Economic Research and Social Change; and the National Council of Arab Americans.<p>
An early highlight: New York City, where on September 14, Galloway will go toe to toe in a debate with former-leftist-turned-prowar-pundit Christopher Hitchens.<p>
Speaking alongside U.S. antiwar activists, Galloway will bring something almost never heard from the mouth of a U.S. politician--an unapologetic stand against war and occupation. <p>

