<font face="Times New Roman, Times, serif" size="5"><b>How colonizers became ''victims'' in Gaza</b></font> <p>
<font face="Arial, Helvetica, sans-serif" size="2">August 26, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THE MAINSTREAM media in the U.S. finally put news of house demolitions in Palestine on the front pages in mid-August.<p>
But not one story was about the nearly 12,000 Palestinian homes that Israel has destroyed since 1967. Instead, the press captured every tiny detail of the demolition of Israeli settlements in Gaza in the run-up to Israel's disengagement, which began this month.<p>
The Israeli government's plan to pull settlers out of Gaza was portrayed as selfless and benevolent. The settlers--far-right fanatics who armed themselves to the teeth--became the tragic victims of a "heartless" policy. Never have the media devoted even a fraction of the time to the suffering of Palestinians jammed into the tiny area of Gaza as they did to the handful of settlers.<p>
Nor did any report point out the absurdity of the settlers--many speaking perfect English because they were born half a world away, in the United States--claiming as their eternal home land that has been lived on, farmed and nurtured by Palestinians for generations.<p>
Israeli Prime Minister Ariel Sharon's government has established a special office to ensure that every last settler finds a new home in Israel. But no compensation has ever been paid to Palestinians whose homes were destroyed since 1967--or the Palestinians who were forced from their homes by the 1948 war of terror that established the state of Israel.<p>
In reality, the withdrawal from Gaza isn't really a withdrawal--nor are Israel's "sacrifices" anything of the sort. Israel will continue to control Gaza's air space, waterways and border crossings, and it has reserved the right to launch military strikes into Gaza at any time. That's why, "for their part, Israeli officials and media have consistently refrained from using the term 'withdrawal' (nessiga in Hebrew) in referring to the pullout, instead using the term 'disengagement,'" according to Khaled Amayreh in Egypt's <i>Al-Ahram Weekly.<p>
</i>The Gaza disengagement won't bring any semblance of justice for Palestinians. Instead, Israel is giving its occupation a makeover that brings with it several advantages.<p>
"For one, Israel can comfortably subtract Gaza's 1.5 million inhabitants from its demographic nightmare, maintaining, for a while longer perhaps, the Jewish majority," writes Ramzy Baroud. "The move will also end Israel's futile military quest to subdue a strategically inconsequential enclave, scrapping with such a decision the unfavorable international attention given to its Gaza occupation, the demoralization of its armed forces and the unavoidable loss of life as a result of Palestinian attacks on its settlements."<p>
Palestinians will surely celebrate Israel's formal exit from Gaza, but they will remain confined in what has been called the world's largest open-air prison. With Israel continuing to deny Palestinians the right to travel outside Gaza or enough control over shipping and transportation of goods to restart their devastated economy, a confrontation with Gaza's Palestinian inhabitants has simply been postponed.<p>

