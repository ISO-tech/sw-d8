<font face="Times New Roman, Times, serif" size="3">The crime of the four MSPs? Mounting a peaceful, silent protest, lasting five minutes, during the first minister's question time. SSP members Colin Fox, Frances Curran, Rosie Kane and Carolyn Leckie held up placards demanding that first minister Jack McConnell carry out the will of the parliament by defending the right to protest peacefully against the G8 leaders at Gleneagles on July 6.<p>
Five months earlier, the Scottish parliament had passed a motion supporting the right of people to protest at the G8 summit. Yet just six days prior to the planned protest, official permission had still not been granted for the protest to go ahead. We believe that this was deliberately done by the authorities, in order to cause confusion and undermine the planned demonstration.<p>
This unprecedented and draconian penalty was imposed through a process which makes a mockery of natural justice. The SSP MSPs were tried in their absence, without any kind of due process, right of appeal or any of the basic human rights that are enshrined in law. This attack is not only targeted at the SSP representatives. By banning them for the month of September, 130,000 voters will be denied a voice in the Scottish parliament.<p>
In the last six years, the SSP has established itself as a tireless defender of working-class interests at home and around the world. It has a proud record of supporting workers in struggle and has been at the forefront of opposition to imperialist aggression in Afghanistan and Iraq. <p>
In 2003, we received 130,000 votes, and six MSPs were elected. The SSP, as the only party in Scotland consistently opposing the neoliberal agenda, has earned the enmity of the mainstream political parties and the political establishment. <p>
Therefore, we cannot let these attacks on democracy, freedom of speech and the right to protest succeed. We are campaigning internationally to bring pressure on the Scottish parliament to overturn the ban and the financial penalties imposed on the SSP MSPs. <p>
Already, messages of support have been pouring in from all over Europe and the world, from members of the European parliament, from trade unions and from individual workers. Please add your voice to the growing campaign.<br>
<b>Colin Fox,</b> Scottish Socialist Party Convenor<p>

