<font face="Times New Roman, Times, serif" size="5"><b>Defend Scottish socialists</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 26, 2005 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">THE SCOTTISH Socialist Party (SSP) is under attack. Four SSP Members of the Scottish Parliament (MSPs) and their staff have been issued with a month-long ban without wages for defending the right to protest against the summit of the G8 leaders in Gleneagles, Scotland, in July. <p>

