<font face="Times New Roman, Times, serif" size="5"><b>A new excuse to target immigrants</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 26, 2005 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">A $448,983 federal grant was extended to the state of Connecticut in order to combat "human trafficking" on July 19. It is no less than an official sanction and material reinforcement of a growing immigrant hunt in the state and nation.<p>
Ostensibly, Gov. Jodi Rell's "primary goal is to rescue trafficking victims. We can do that by assuring that our law enforcement officials receive proper training." Commissioner of Public Safety Leonard Boyle said the money will train officers to, among other things, locate people living in overcrowded housing. Boyle's remarks come in the wake of a residential fire in Danbury, Conn., which revealed 37 Latinos living in a building zoned for 25.<p>
Fairfield County in southwestern Connecticut, of which Danbury is a part, boasts an 11.9 percent Latino population, the highest in the state. Boyle has since made a point of talking specifically about this area in the media. That Boyle made the effort to distinguish the northern mega-rich, bucolic, cosmopolitan suburbs of New York City from southern ghettoized cities like Bridgeport highlights the issue of class that beats at the heart of this question.<p>
Furthermore, the terms of Rell's argument for this program are consciously misleading. "Trafficking victims" are, by standard definition, mail-order brides, prostitutes and debt peons. The reality of life for most of the undocumented in Connecticut is being underpaid and overworked in restaurants, hotels, laundromats, landscaping, construction, housecleaning and child care.<p>
This extension of police powers is therefore only a thinly veiled endorsement of racial profiling and a blatant denial of civil rights for a fundamental and growing sector of Connecticut's economy. Where trafficking victims do exist in the state, they will surely remain--while police surveillance and harassment of immigrants generally will undoubtedly increase.<p>
Tellingly, Paul Streitz, co-director of Connecticut Citizens for Immigration Control (CCIC), a local offshoot of the Minutemen movement, has called Rell's program "a good start." In a June issue of the <i>American Conservative</i>, Streitz predicted it was only a matter of time before elected officials would catch up with him and his organization. He has proven eerily prescient.<p>
Yet on the ground, CCIC meetings have been consistently outnumbered by double and triple their number in immigrant-rights protesters. <p>
All this also follows on the heels of Danbury Mayor Mark Boughton's failed attempt to deputize state police as immigration agents. He was resoundingly beaten back by a march of some 1,300 activists.<p>
The strong front of immigrant rights, Palestinian rights, religious, antiwar and socialist groups who have shown themselves willing to fight are the only solution to this state-sponsored terrorism.<br>
<b>Max Clark, </b>New Haven, Conn.<p>

