<font face="Times New Roman, Times, serif" size="5"><b>Pittsburgh police attack counter-recruitment protest</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Michelle Gaffey and Tina Gross</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 26, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">PITTSBURGH--Using pepper spray, taser guns, retracting batons and dogs, police attacked antiwar protesters as they gathered on August 20 as part of a "non-violent day of direct action" against military recruitment and the war on Iraq. Five people were arrested, two were sent to the hospital--one who had been tasered and another who was bitten by a police dog--and several others were injured.<p>
About 150 protesters, including parents with young children, marched to an armed forces recruitment center in the Oakland neighborhood to celebrate the fact that recruiters had decided to close down that day in anticipation of the protest called by the Pittsburgh Organizing Group (POG). The same recruiting station had been the target of protesters just two weeks earlier.<p>
When a dispute broke out between protesters and an antagonistic photographer from Fox News, police began chasing protesters and throwing them to the ground. Pepper spray and tasers were used on several people, and a protester with multiple sclerosis in a wheelchair scooter was knocked over when police charged the crowd. "The response was way over the top," POG spokesperson David Meieran told the Associated Press.<p>
Indymedia video shows police tasering a woman as she lay on the street screaming. A 68-year-old grandmother was bitten from behind by a police dog, and then arrested when she told police she wanted to file a complaint. According to a POG press release, police put her in an unventilated police van in the hot sun for 45 minutes before she was finally taken to the hospital for treatment.<p>
University of Pittsburgh police on the scene reportedly told the crowd that any Pitt students arrested would be expelled.<p>
While the police have shown that they will go to any extreme to intimidate the counter-recruitment movement, activists are determined to continue their struggle against the U.S. war machine. "Our goal for today was to shut down military recruitment for that station, and to the extent that they may have shut down pre-emptively, we achieved our goal," Meieran said.<p>

