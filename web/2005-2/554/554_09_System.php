<font face="Times New Roman, Times, serif" size="5"><b>System of a Down's furious challenge</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Alessandro Tinonga</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 26, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">System of a Down, <b><i>Mezmerize</b>, </i>released by American Recordings.</font><p>

<font face="Times New Roman, Times, serif" size="3">"WHY DON'T presidents fight the war?/ Why do they always send the poor?" cries Serj Tankian of System of a Down on "B.Y.O.B." from the recently released album <i>Mezmerize</i>. <p>
Once again, the socially conscious metal band has unleashed a record of musical and political fury that challenges both America's foreign policy and culture. <p>
The band tears straight into the contradictions of the U.S. war on Iraq. "Soldier Side" and "B.Y.O.B." ask why America's poor and working people fight the war, while the elites sit back and benefit. With lightning fast guitar riffs and somewhat pop-style lyrics, "Sad Statue" speaks to the polarized U.S. political situation, in a country that "turns a deaf ear to the cries of human suffering."<p>
Keeping true to their name, the band is critical of the entire system. "Radio/Video" attacks the myth of the American Dream that survives and thrives in pop culture: "Hey man!/ Look at me, rockin' out/ I'm on the radio." "Old School Hollywood"/"Lost in Hollywood" try to wash away the perception of Los Angeles as a city of hopes and dreams--showing instead that it is a vicious arena where people are used up and spat out by big-shot executives. <p>
Serj Tankian's soaring vocals, Daron Malakian's beautiful guitar riffs, and the amazing beats produced by drummer John Dolmayan and bassist Shavo Odadjian have captured the ears of millions of music fans. <p>
However, many will miss the album's lyrics and political message. Since the release of System of a Down's first album, the band has continued to be explicit in their political beliefs. Their numerous songs have covered topics as diverse as the Armenian genocide by the Turkish government, the criminalization of marijuana, the military-industrial complex, the commoditization of sex, the war on Iraq and even revolution. <p>
The group's members have also taken their politics beyond the stage. Most notably, Serj Tankian co-founded with former Rage Against the Machine guitarist Tom Morello the activist network Axis of Justice. Check out their Web site at <a href="http://www.axisofjustice.org" target="_blank">www.axisofjustice.org</a>. <p>
<i>Mezmerize</i> is an outstanding political album that all antiwar activists, whether or not they are rock fans, can appreciate. <p>

