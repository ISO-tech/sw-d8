<font face="Times New Roman, Times, serif" size="5"><b>Dancing that gives voice to LA's rage</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Justino Rodriguez and Nick Bergreen</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 26, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Rize</b>,</i> directed by David LaChapelle.</font><p>

<font face="Times New Roman, Times, serif" size="3">"THIS IS our neighborhood. This is where we grew up. We managed to grow from these ashes. And this is where we still live."<p>
The movie <i>Rize </i>begins with images of Los Angeles in turmoil and transition: the 1965 Watts rebellion, Martin Luther King Jr. on the march, the 1992 uprising following the acquittal of the cops who beat Rodney King. From these ashes rose a style of dancing known as clown and krump dancing--as a way to articulate the frustration and anger of young, poor and predominantly African American residents of South Central LA.<p>
The film introduces Tommy the Clown, whose dancing at children's parties attracted other youth, who formed clown-dancing crews as an alternative to gangs. As clown dancing grew, it evolved in different directions, one being krump dancing. "If you're drowning, and you reach out, and there's nothing around but a board floating, then you're going to reach out for that board," explains a dancer named Dragon. "This is our board."<p>
In one scene from <i>Rize</i>, three dancers mimic a scene of police brutality with krump dancing--with the victim eventually breaking free.<p>
The film, while powerful, has some scenes that seemed divorced from the rest of it. The choice of the director, fashion photographer David LaChapelle, to include footage of African tribal dancing is a weak attempt to draw a link that doesn't exist and could be viewed as racist.<p>
However, what shines the brightest in the film is the dancers' everyday battles. Their story is that of many people in America, and krump gives a voice to this story.<p>

