<font face="Times New Roman, Times, serif" size="5"><b>A war on immigrants</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 26, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>JEFF BALE explains what's behind the border "emergency" declared in Arizona and New Mexico.</b><p>

ARIZONA GOV. Janet Napolitano joined New Mexico Gov. Bill Richardson last week in declaring a "state of emergency" on the border with Mexico. But these two Democrats were really declaring war--on immigrants.<p>
The U.S. government's toughened border policy has caused a record number of deaths this year among immigrants making the journey through the rugged Sonoran Desert. The survivors and their families face violence and exploitation by mafia-style smuggling organizations that prey on people desperate to get into the U.S. And there is the emerging threat of armed vigilantes like the so-called Minutemen, who have taken it on themselves to "patrol" the border.<p>
But that's not the emergency, according to Napolitano and Richardson. Instead, the two have joined the growing anti-immigrant hysteria by complaining about an alleged increase in property destruction, crime and violence committed by undocumented workers. "The situation is out of hand," Richardson told CNN host Lou Dobbs.<p>
In declaring states of emergency, Napolitano and Richardson released $1.5 million and $1 million respectively to pay for more law enforcement jobs, as well as more overtime for current officers.<p>
Both governors made passing mention of the increased violence associated with human smugglers. But their focus was primarily on stolen cars, stolen and dead cattle, and vandalism on private ranch property. <p>
In fact, according to many residents along the border, this "crime" and "vandalism" is mostly limited to people breaking into ranch properties to look for water and extra clothing to survive the desert.<p>
Richardson also reinforced the unproven claim that "terrorists" are exploiting easy passage across the U.S.-Mexico border by calling on the Office of Homeland Security to open a field office on the New Mexico border. In turn, Napolitano added to the drama of her announcement by holding a joint press conference with her counterpart from the Mexican state of Sonora--where she once again stressed increased law enforcement.<p>
Both governors are out to score political points by claiming that the "federal government"--meaning the Bush administration--is failing to protect the border. Napolitano is up for re-election next year and is facing an emboldened right wing that has made a big push around the issue of immigration. <p>
But the political posing goes further than next year's election. Napolitano and Richardson are being hailed by national Democratic leaders as models of how their party can be popular in the so-called "red states." <p>
The declarations of a state of emergency show what this strategy really amounts to: an attempt to overcome the Republicans' support among conservative voters by outflanking them to the right.<p>
The Democratic establishment believes that George Bush is vulnerable on immigration because of his floated proposal of a temporary "guest worker" program--with the support of Republican business interests that want cheap labor in the fields and on the shop floor. The Democrats' "alternative" is to hype a fake immigration "crisis" and propose tougher border control measures.<p>
Ultimately, this aids and abets the growth of far-right vigilante groups like the Minutemen. The Minutemen's first foray at the border in Arizona last April has produced copycat groups all over the country--escalating the immediate threat of violence that immigrants, undocumented or not, face everywhere.<p>
While Minutemen leaders claim to be non-violent volunteers, a recent court case highlights the real danger they represent. In a surprise victory for immigrant-rights supporters, two El Salvadoran immigrants were recently awarded a ranch that belonged to Casey Nethercott, a leader among Arizona vigilante groups--as a settlement for being taken hostage and abused.<p>
When politicians like Napolitano and Richardson call for tougher borders, they guarantee that more Nethercotts will arise to terrorize immigrants. It will be up to immigrant-rights supporters and antiracist activists at the grassroots to stand up to the vigilantes--and to the politicians who give the green light for their racism. <p>

