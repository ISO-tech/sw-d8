<font face="Times New Roman, Times, serif" size="5"><b>Birth control bill vetoed in New York</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Leia Petty</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 26, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK Gov. George Pataki vetoed landmark legislation that would have made emergency contraception available without age or parental restrictions.<p>
The New York state Senate passed a bill allowing emergency birth control to be issued without a prescription. But in an attempt to flex his right-wing muscle for the 2008 presidential campaign, Pataki vetoed it shortly after it got to his desk.<p>
Already, 25 percent of pharmacies in New York state don't carry emergency contraception, meaning that even with a prescription, access is limited. And thanks to the "Workplace Religious Freedom Act," cosponsored by Democrat John Kerry, and mobilizing by right-wing groups like Pharmacists for Life, birth control is under attack nationally.<p>
The right wing is using restrictions on emergency contraception as part of a wider attack on women's ability to make the most basic decisions about their health. But they aren't going unchallenged.<p>
To put pressure on Pataki, pro-choice groups, including NARAL Pro-Choice America, Planned Parenthood and the National Latina Institute for Public Health, mobilized over 150 people to protest outside of the governor's office on the day of the veto. "We won't stand for Pataki playing political games with women's lives," said one activist.<p>
We need a movement throughout New York and the U.S. to demand full reproductive rights--no apologies, no restrictions. <p>


