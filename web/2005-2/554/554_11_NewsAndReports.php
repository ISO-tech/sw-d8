<font face="Times New Roman, Times, serif" size="4"><b>Defend abortion rights</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Justin Powers and Alden Eagle</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--About 70 people met August 14 to kick off the Bay Area Coalition for Our Reproductive Rights (BACORR). Many activists were fresh from several Bay Area clinic defenses organized recently to thwart the bigoted anti-abortion group "Crusade for Life" from shutting down local Planned Parenthood clinics.<p>
Participants discussed the urgency of opposing Proposition 73, a state constitutional amendment that would require girls 17 and younger get parental permission before obtaining an abortion, which is scheduled to be on California's special election ballot in November. They also discussed how to protest Bush's nomination of right winger John Roberts for the Supreme Court.<p>
The initial points of a unity document circulated at the meeting stated in part that "BACORR is committed to help build a militant broad-based activist women's movement, like the one that won abortion rights in 1973."<p>
The mood in the room was serious as everyone agreed that, over the course of the next couple of years, a woman's right to have an abortion and <i>Roe v. Wade </i>hang in the balance. "The thought of loosing my ability to have an abortion scares me to death," said a participant named Susan. "I have to do something. We have to do something."<p>
Tentative plans have been made for a September Abortion Rights conference, a fall demonstration against Prop 73 and in support of <i>Roe v. Wade, </i>emergency clinic defenses if needed, and outreach into the community through tabling and letter-writing campaigns.<p>

<i>For more information about BACORR, contact: <a href="mailto:lichi_d@yahoo.com" target="_blank">lichi_d@yahoo.com</a></i>.<p>

-- In <b>Providence, R.I., </b>more than 20 pro-choice activists held a lively picket outside the office of Rhode Island Sen. Lincoln Chafee on August 19. Although Chafee is a pro-choice Republican and is even endorsed by NARAL Pro-Choice America, he has sent strong signals that he's voting for Bush Supreme Court nominee John Roberts.<p>
Protesters chanted "Not the church, not the state--women must control their fate!" and a few passersby who saw the picket joined in immediately.<p>
Activists plan to launch a coalition to defend abortion rights out of this protest. The need for grassroots pro-choice activism in Rhode Island is clear. The state's two clinics are constantly patrolled by right-wing bigots, and a woman was recently refused emergency contraceptives at a CVS pharmacy. <p>

