<p><a name="Racism"></a><font face="Times New Roman, Times, serif" size="4"><b>Stand up to anti-Arab racism</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By John Osmand</b></font><p>

<font face="Times New Roman, Times, serif" size="3">PROVIDENCE, R.I.--On August 10, more than 30 people attended a meeting by University of Rhode Island (URI) professor Nasser Zawia sponsored by the RI Community Coalition for Peace (RICCP).	Zawia covered a gamut of topics, from the history of Western imperialism in the Middle East and Zionism to the scapegoating of Arabs and Muslims.<p>
Less than a week before the meeting, a fire at the home of a Muslim chaplain at URI raised concerns that his family may have been the target of anti-Arab violence, although the investigation is still pending. Ali Hassan of the RI Council for Muslim Advancement (RICMA), who was at the discussion, has since met with authorities in an effort to ensure a full investigation into the fire.<p>
Several members of the Arab and Muslim community were present at the lecture. Several attendees agreed to promote the September 24 march on Washington against the occupation of Iraq, pledging to publicize RICCP's bus transportation to D.C. at every area mosque. Contributions totaling over $100 were raised at the meeting to help fund the mobilization effort.<p>
<i>For information about RICCP, go to <a href="http://lists.riseup.net/www/info/ccp" target="_blank">http://lists.riseup.net/www/info/ccp</a>. For information about RICMA, go to <a href="http://ricma.org" target="_blank">http://ricma.org</a></i>.<p>

