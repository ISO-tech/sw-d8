<p><a name="SC"></a><font face="Times New Roman, Times, serif" size="4"><b>South Central farmers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Bill Neal</b></font> <p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES--About 100 protesters representing South Central's urban farmers gathered at City Hall July 29 to prevent the city's sale of their land.<p>
Developers with connections to city council member Jan Perry recently won the legal right to buy the property from the city. But low-income community members have farmed the public land for a decade and a half under an agreement going back to the administration of Mayor Tom Bradley. If evicted, organizers say 347 families will be affected.<p>
The 14-acre tract of land has been the site of nearly 20 years of struggle between the community and developers. The land was originally slated for construction of a trash incinerator, but protests halted the project.<p>
After the 1992 LA Rebellion, permission to use the land for gardens was granted after a deal struck with a local food bank director. The land came to be widely used for growing supplemental food for poor residents, many of whom are from Latin America, especially Mexico.<p>
The gardeners are fighting back through protest and a legal strategy that is taking them to the California Supreme Court. The South Central farmers deserve everyone's support.<p>

