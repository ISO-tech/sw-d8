<font face="Times New Roman, Times, serif" size="5"><b>False stories of murder and mayhem</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 7, 2005 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">THE STORIES of murder, rape and mayhem that emerged from the Superdome and New Orleans Convention Center during the Katrina disaster were horrific beyond belief. They were repeated endlessly, on cable TV news shows and in respected newspapers alike.<p>
And they turned out to be false. Officials in charge of the aftermath at the two "shelters of last resort"--where poor, predominantly Black residents were herded together as the hurricane hit--agree that there is no evidence of the horrific carnage reported in the media.<p>
Louisiana National Guard Col. Thomas Beron told the New Orleans <i>Times-Picayune </i>that a doctor from the Federal Emergency Management Agency came to the Superdome with a refrigerated 18-wheeler, prepared to process as many as 200 corpses.<p>
The real total was six, Beron said. Four died of natural causes, one died of an overdose, and another jumped to his death in an apparent suicide, he said. None had been murdered. At the convention center, four bodies were recovered, only one of which appeared to have been slain, according to authorities.<p>
Orleans Parish District Attorney Eddie Jordan rightly denounced the national media for spreading the Superdome horror stories. "It's unfortunate we saw these kinds of stories saying crime had taken place on a massive scale when that wasn't the case," Jordan said. "And [national media outlets] have done nothing to follow up on any of these cases; they just accepted what people told them."<p>
But Jordan might have saved some criticism for his colleague, New Orleans Police Chief Eddie Compass. In interviews with Oprah Winfrey among others, Compass claimed that "babies" had been raped in the Superdome and that his officers had been involved in multiple shootouts there and at the Convention Center.<p>
While the corporate media quickly spread the false stories about New Orleans residents acting like animals, they were much slower to report on instances when the authorities acted like animals.<p>
For example, the eyewitness account--written by San Francisco Emergency Medical Services workers Larry Bradshaw and Lorrie Beth Slonsky, and first published in <i>Socialist Worker</i>--of a confrontation between gun-toting Gretna, La., sheriff's deputies and hundreds of people desperate to get out of New Orleans wasn't mentioned in the mainstream media for close to two weeks after it happened. As Bradshaw and Slonsky described--and Gretna authorities have since admitted--the cops stopped people from self-evacuating over a bridge that crosses the Mississippi River, even firing over their heads to get them to turn back.<p>
Even after mainstream papers like the <i>San Francisco Chronicle </i>and <i>New York Times </i>corroborated Bradshaw and Slonsky's account, other media outlets justified their lack of coverage on the grounds that the story had emerged on the Internet--or from a left-wing source like <i>Socialist Worker</i>.<p>
Not from a "reputable" source like Police Chief Eddie "Superdome Shootout" Compass.<p>

