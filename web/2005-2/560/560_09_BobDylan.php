<font face="Times New Roman, Times, serif" size="5"><b>Unraveling the myth of Bob Dylan</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Donny Schraffenberger</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 7, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Bob Dylan: No Direction Home, </b></i>directed by Martin Scorsese, originally aired on PBS's <i>American Masters</i>, available on DVD.</font><p>

<font face="Times New Roman, Times, serif" size="3">MARTIN SCORSESE'S new documentary, <i>Bob Dylan: No Direction Home,</i> is an explosion of sight and sound from the first five years of Bob Dylan's musical career.<p>
With never-before-seen footage, Scorsese shows the young Robert Zimmerman--soon-to-be Bob Dylan--growing up in the cold, desolate north country of small-town Minnesota. We witness the tales of a cherub-cheeked Bobby skipping classes at the University of Minnesota to monomaniacally pursue his obsession with music.<p>
The documentary interviews and recalls record executives, folk personalities and such luminaries as Allen Ginsberg and Johnny Cash. We get the insightful musings of Joan Baez, even though she still seems to hold a grudge against Dylan. Along with Mike Marqusee's <i>Chimes of Freedom: The Politics of Bob Dylan's Art </i>and Dylan's own autobiography <i>Chronicles: Volume One,</i> <i>No Direction Home</i> helps illuminate the murkiness of the Dylan myth and set him in a historical context.<p>
Before Dylan was a "folk singer," he was interested in a wide range of music that traveled long distance over the nighttime AM radio stations. Country, blues and early rock and roll brought a new world to the teen, whose town was saturated with Doris Day dregs and stilted commercial schmaltz.<p>
Dylan played in rock bands and identified with the beats and Jack Kerouac's novel <i>On the Road.</i> But then he got into folk music, which was sweeping college campuses in the late 1950s and early '60s. Dylan was like a sponge, absorbing a wide range of books, from Carl Von Clausewitz's <i>On War </i>to the poetry of William Blake.<p>
But what really juiced him up was Woody Guthrie's autobiography <i>Bound for Glory</i>. Dylan mimicked Guthrie's voice and dress. He even traveled to New York City to visit Guthrie several times in a hospital, where he was slowly dying from Huntington's disease.<p>
Dylan was one of many musicians who gravitated to New York City's Greenwich Village folk scene from around the country. But unlike most, he got lucky and was discovered early on by Columbia talent scout John Hammond.<p>
Dylan's first album was mostly cover songs, but he emerged as a songwriter on his second LP, <i>The Freewheelin' Bob Dylan</i>. The follow-up political hard-hitter <i>The Times They Are A-Changin'</i> included the antiracist "Only a Pawn in Their Game" and the rousing "When the Ship Comes In."<p>
Dylan played at the 1963 March on Washington. He traveled to the South to show his support for civil rights. And along the way, the press called him the spokesman of his generation.<p>
Dylan says he recoiled at such a label. He saw himself as a musician first--a musician who didn't want to be held in chains by the confines of the folk scene or the old left. Rightly or wrongly, he broke the conservative musical chains and opened up new parameters for popular music.<p>
Being ahead of his time, he was denounced by folk purists. The 1965 Newport Folk Festival has created a myth so hard to untangle that even Scorsese's interviews only confirm that no one has the same take on what happened.<p>
Dylan showed up with an electric guitar and a band. On this, everyone agrees. But the crowd and other performers' reactions have been debated for decades. How much was Dylan booed? Did Pete Seeger really try to cut the electric cables with an ax? Seeger says no; others repeat the story.<p>
The footage of English audiences' diverse opinions after hearing an electric Dylan set is brilliant. Also, the documentary gives us a good glimpse into Dylan's perspective, and his burnout when he's asked the same inane questions over and over again by the media. It could have been better if it had included some of Dylan's important contemporaries and rivals, like Ramblin' Jack Elliott or the "could have been a contender" Phil Ochs.<p>
Check out the three great records--<i>Bringing It All Back Home, Highway 61 Revisited </i>and<i> Blonde on Blonde</i>.<p>
The beauty of Dylan was that his music was exciting and challenging. He helped to expose the hypocrisy of mainstream society's ruling elites. The problem was and still is that he doesn't have the political answers.<p>
He reached his musical creative peak in 1966, where Scorsese ends the film. He has remained ever since--but appearing more in the sky like a musical Halley's comet than a blinding supernova.<p>

