<font face="Times New Roman, Times, serif" size="4">Strike pushes back Boeing but more could have been won</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>IAM "only sold positives"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Darrin Hoop</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 7, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">THE 18,500 members of the International Association of Machinists (IAM) voted September 29 to end their 28-day strike against the Boeing Co. by what union officials reported as an 80 percent vote in favor. The deal was struck only days after Boeing brought in a paid consultant--none other than Richard Gephardt, the former Democratic minority leader in Congress who was endorsed by the IAM in his bid for president last year.<p>
Boeing, IAM District 751 President Mark Blondin and the mainstream media in Seattle all gushed about the contract, which covers workers in the Puget Sound area, Wichita, Kan., and Gresham, Ore.<p>
The strike displayed the workers' power--and forced a tough-talking management to drop its harshest demands on health care and give ground on other issues. But longtime rank-and-file activists argued that the union let Boeing off the hook--and could have won much more.<p>
David Clay, a jig builder and a 28-year employee of Boeing's Everett plant, was especially critical of Gephardt's role. "Gephardt<b> </b>was brought in on these negotiations because he was successful in assisting Boeing in the sale of its Wichita Division" earlier this year, he said. "This resulted in a 10 percent wage cut and other huge takeaways from the machinist union members. He was rewarded with a seat on the board of directors at the company that bought Boeing Wichita, Spirit/Onex."<p>
The three-year contract contains a 16.7 percent increase in pension benefits, raising from $66 to $70 the amount Boeing would pay workers per year of service upon retirement. Boeing also dropped its demands that workers pay annually between $2,000 and $4,000 for health care--as well as a demand to eliminate retiree health care for new hires. Management also agreed to part of the union's proposal to limit outsourcing, particularly over who delivers parts to the assembly lines and how factory team leaders are picked. <p>
However, pension and language gains are minimal. While the current health plan was protected, it was considered concessionary when it was accepted as part of the 2002  health plan.<p>
Moreover, <i>Business Week </i>reported that Boeing could have met the IAM's demand for a pension multiplier of $80 for just $90 million more over the three-year life of the contract. The magazine commented that Boeing was enjoying some of its best airplane sales ever--giving the company $5 billion in cash and $1.1 billion in profit in the first half of this year. <p>
The contract also includes no general wage increases (GWI). Instead, union members will receive an immediate bonus of 8 percent, based on earnings over the 12 months prior to the strike. However, many IAM members who were laid off and recently rehired will see little money on that basis. Lump-sum payments over the following two years of the contract will be more modest--$3,000.  <p>
As Don Grinde, a crane operator and another 28-year union activist at the Everett, Wash., plant, commented, "There's no commitment on job security. There's still potential for thousands of more jobs to be lost. Reality will soak in later when the bonus money is gone, and people get outsourced. The bonus money and the 2.5 percent GWI we normally get in our contracts could have bought a $115-a-month benefit in the IAM National Pension Plan. Union leadership only sold the positive side of the offer and down played or flat out ignored the downsides. They abandoned job security, outsourcing language and the union pension plan."<p>
The original 86 percent vote to strike and the strike itself show the tremendous anger that exists among the machinists and the potential to rebuild labor. But the speed at which Boeing, Gephardt, and the IAM moved to cut a backroom deal and work together with the corporate media to sell it highlights the importance of developing organization independent of the IAM leadership. <p>
As Grinde put it, "We need to build a coalition of independent voices and a communication network so we can communicate during the event of a strike in the future."<p>

