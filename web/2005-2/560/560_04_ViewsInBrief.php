<p><a name="Criminals"></a><font face="Times New Roman, Times, serif" size="4"><b>Criminals at the top</b></font><p>

<font face="Times New Roman, Times, serif" size="3">PFC. LYNNDIE England was sentenced to three years in prison for her role in torturing detainees at Abu Ghraib prison in Iraq.  <p>
The process of punishing England for carrying out her boss' wishes has amounted to little more than a publicity stunt. Our government would have us believe that England is to blame for this incident, and that the people who created this quagmire and sanctioned torture are blameless.<p>
The real criminals work in the White House and Pentagon, but they will never see the inside of a prison cell unless we organize and fight back.<p>
We can't keep our hopes pinned to the Democrats, because despite all of Bush's lies and crimes, they lack the will to oppose him. Like the Republicans, they are part of a sick system that enables the minority who run society to profit from war while the rest suffer. <p>
If we really want to see justice done, we need to build a mass independent movement that can hold the criminals at the highest levels of our government accountable and force the U.S. to leave Iraq. Let Iraqis determine their own future, free from torture, bombings and occupation!<br>
<b>Nicholas Hart,</b> Seattle<p>

