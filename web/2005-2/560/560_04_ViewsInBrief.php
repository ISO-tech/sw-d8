<p><a name="Our"></a><font face="Times New Roman, Times, serif" size="4"><b>Invading our campuses</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I AM a student at the University of Wisconsin (UW) at Madison. Our university has several fitness facilities, as well as group exercise classes. In spring 2005, UW-Madison started a class entitled "Boot Camp," held Tuesdays and Thursdays. I joined the class because I enjoy a good workout. The teacher was very friendly. She was a graduate student and non-military. <p>
Beginning this fall, I found out they would hold the class again. When I first walked into the class, I was shocked. The graduate student teacher had been replaced by two military recruiters--one Marine, the other Army--in fatigues. <p>
The class was very different. We were yelled and screamed at, and the exercises had changed a lot--from running sprints to running in formation with metal poles (which the instructors call machine guns), climbing sand pits low to "avoid fire," jumping walls and covering our buddy from exploding "shells." Other exercises included carrying our partners, which they refer to as "casualties," climbing walls, and continuously doing the cadence. <p>
As a veteran member of the antiwar movement and a counter-recruiter, I was very upset. I was confused on what to do--whether I should just not show up this semester and allow my fellow classmates to be openly recruited for cannon fodder with no opposition, or show up and try to be a counter-recruiter. <p>
I decided to counter-recruit, although I am fearful since I am alone, and could be hurt easily, due to the dangerous exercises. I am also angry, because I feel the class has degenerated in quality since the previous spring. Instead of going through an intense workout, we now spend a lot of time doing marching and listening to military talk. <p>
This week, I showed up with a shirt which has a picture of an Army soldier saying "free college tuition" jokingly, and has a huge peace sign on the back. During the class, one recruiter the Marine came up and said to me, "This is not a peace camp." Throughout the class, they have seemed to increase the military rhetoric and continually talk about how essential the things we are learning could be in a terrorist attack. In addition, they openly attempt to recruit for the campus ROTC. <p>
I have tried to find other antiwar activists to go with me as a safety net and additional help--however, due to the intensity and time of the class I have had no luck. I am looking for a willing student. <p>
I guess this just displays how the military has decided to change their recruiting tactics.<br>
<b>Brian, </b>Madison, Wis.<p>

