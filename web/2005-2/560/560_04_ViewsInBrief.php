<font face="Times New Roman, Times, serif" size="4"><b>Right wing on the defensive</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I WAS taking the train back from the inspiring antiwar demonstration on September 24, packed mostly with fellow protesters, when I saw three Bush supporters also on the train. One of them had a T-shirt that said, "Protest warrior," which is the name of the group that harasses and ridicules antiwar protesters. Another one wore a button that read, "Save America. Vote Republican." <p>
I wondered how anybody could still believe that, especially after Bush's incompetent handling of the Hurricane Katrina disaster. So I got into a debate with them. I asked one of them if the number of lives lost in Iraq was acceptable to them. Her callous response was to say that more people die in car accidents than due to the war in Iraq! <p>
I told her there's no logic in comparing car <i>accidents</i> to Bush deliberately putting soldiers in harm's way. Many other antiwar activists jumped into the debate at this point, with one woman sitting behind me saying she could see one relationship between the analogy--in both cases people are dying for no reason. <p>
The pro-Bush crew argued U.S. troops are dying for a reason--for freedom! They didn't make clear whose freedom. But I pointed out Paul Wolfowitz, who was part of Bush's administration, said the reason the U.S. attacked Iraq was that "the country swims on a sea of oil."<p>
At this point, all the Republicans could say was that since we're leaving the protest, we should forget about politics for now, go home and have a beer. Easy for them to say--they don't face death by a roadside bomb or a U.S. missile reigning down on their house any time soon. <p>
When the Bush supporters got off the train, we felt confident that the facts are behind us, while the right-wingers increasingly have less and less justifications for their policies.<br>
<b>Dominic Renda, </b>New York City<p>

