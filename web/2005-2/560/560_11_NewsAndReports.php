<font face="Times New Roman, Times, serif" size="4"><b>Stop the Minutemen</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Ben Dalbey</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WASHINGTON--With only one day's notice, about 30 activists mobilized to protest the presence of racist vigilantes Chris Simcox, president of the Minutemen Civil Defense Corps, and James Gilchrist, founder of the Minutemen, on Capitol Hill September 28.<p>
The Minutemen have recently floundered in their efforts to mobilize armed civilian militias to patrol the U.S. border with Mexico in Arizona and California after activists in the region repeatedly confronted the vigilantes and politically exposed their far-right ideology and fascist supporters.<p>
The D.C. event, held in the swank Capitol Hill Club, a private Republican club a stone's throw away from the U.S. Capitol, was billed as a "kick-off" for the vigilantes' "Secure Our Border" national initiative planned for the month of October.<p>
The business-suited congressional staffers and Capitol Hill elite arriving at Gilchrist and Simcox's reception were visibly surprised and upset to see a young, confident and vibrant crowd chanting with a bullhorn, carrying picket signs, and leafleting at their front door. Unable to have the police remove the protesters from the public sidewalk, the high-class racists resigned themselves to hurling occasional vulgarity and other highly original jibes such as "you smell" as they scurried into the club door.<p>
Even in the heart of Capitol Hill, the counterprotest drew some support and thanks from passersby, including immigrants who work and live in the area. But even more importantly, the small but confident anti-racist presence was enough to strike fear into the bigots' hearts.<p>
They do not want to hear or see us, because in their delusional far-right fantasy, we shouldn't exist. That's why we need to be screaming in their faces everywhere we can.<p>

