<p><a name="Justice"></a><font face="Times New Roman, Times, serif" size="4"><b>Justice for Filberto Ojeda R&iacute;os </b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Eva Mar&iacute;a Woods Peir&oacute;</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--About 300 people gathered in front of Federal Plaza, chanting "Todo boricua, machetero" (All Puerto Ricans are macheteros), last week to protest the September 23 FBI assassination of independence fighter Filiberto Ojeda R&iacute;os.<p>
The 72-year-old Ojeda R&iacute;os, leader of the Machetero group, had been living underground in a farmhouse in Hormigueros, Puerto Rico, when he was shot by an FBI sniper. The FBI barred entry to his lawyer and his family for a full 24 hours, during which time Filiberto bled to death. <p>
He was killed on September 23, the anniversary of El Grito de Lares insurrection--1868 the Puerto Rican uprising against Spanish rule.<p>
Although this political assassination aimed to weaken the spirit of pro-independence Puerto Ricans in their struggle against U.S. imperialism, the opposite has happened. This past week, tens of thousands of Puerto Ricans have protested on the streets and the airwaves of San Juan and Hormigueros and in areas controlled by the U.S. government. And tens of thousands attended the memorial and funeral services of Ojeda R&iacute;os, believed to be one of the largest funerals in Puerto Rican history. <p>
More than 1,000 vehicles took part in a caravan from San Juan to the eastern town of Naguabo where he was buried, according to the New York-based Puerto Rican newspaper <i>El Diario. </i>And organizations from more than 60 countries expressed their solidarity with Puerto Rico in an approved speech at the Asamblea Mundial de Jubileo Sur and the Encuentro Mundial Sur/Norte.<p>
Puerto Ricans of all political backgrounds, including many previously unsympathetic to the Macheteros, have united to mourn and condemn this attack on Puerto Rico's right to self-determination.<p>

