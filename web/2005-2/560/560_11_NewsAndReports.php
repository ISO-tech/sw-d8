<p><a name="William"></a><font face="Times New Roman, Times, serif" size="4"><b>Protest William Bennett</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Shane Johnson</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CINCINNATI--Right-wing commentator William Bennett canceled his appearance at the University of Cincinnati in response to protests planned in reaction to his racist outburst on his radio show last week.<p>
The College Republicans had planned the October 4 event for some time, but as pressure mounted on Bennett, he canceled, saying that the controversy would overshadow his speech entitled "Politics, war and culture."<p>
Bennett claims his comment about aborting Black babies to reduce the crime rate was taken out of context and that he's been "misunderstood." But given Bennett's long history of promoting racist myths, he's not misunderstood. <p>
Groups opposed to Bennett's agenda of hatred called for a protest in front of the student center where he was scheduled to speak. Their message was clear--"No bigots on our campus." Directly confronting the right's racist agenda anywhere it appears is a step forward in the struggle for justice.<p>

