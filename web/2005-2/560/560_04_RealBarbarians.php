<font face="Times New Roman, Times, serif" size="5"><b>The real barbarians in Iraq</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 7, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">THE MEDIA spills a lot of ink every time a video is released by one or another resistance sect, showing grisly abuse of journalists or private contractors in Iraq. But what will they have to say when it comes out that American soldiers have, as the <i>East Bay Express</i> reports, "been using pictures of disfigured Iraqi corpses as currency to buy pornography?"<p>
According to amateur porn "entrepreneur" Chris Wilson, the administrator of the appropriately titled NowThatsFuckedUp.com, soldiers complained that they had problems buying memberships to his service. <p>
"They wanted to join the site, the amateur wife and girlfriend site," he says. "But they couldn't, because the addresses associated with their credit cards were Quackistan or something; they were in such a high-risk country that the credit card companies wouldn't approve the purchase." <p>
The solution? Send in pictures of dead Iraqis in exchange for free access. One example, according to the <i>Express</i>, shows "a head obliterated by gunfire, the flaps of skin from his neck blooming open like rose petals." Such pictures are then accompanied with captions like "Die Haji Die" and "What every Iraqi should look like." The grisliest pictures are posted on a free section of the Web site.<p>
It's hard to find words to express the horror of this practice, and yet it appears that the burgeoning photographers have the fortitude to take it in stride. One soldier said, "Obviously, these postings do not help our public image at all, however, I believe the U.S. has been far too concerned about our public image as of late...we need to take a much harsher stand against these Islamic fundamentalists and stop giving them the royal American treatment." So much for the war of civilization over barbarism. <p>
As they say, a picture is worth a thousand words. These pictures are screaming to us to demand and end to the brutal occupation, because the real threat to Iraqi civilization comes straight from the USA.<br>
<b>Kevin Prosen, </b>Durham, N.C.<p>

