<font face="Times New Roman, Times, serif" size="4">Antiwar activists found innocent of conspiracy charge</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A victory for the St. Patrick's Four</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Brian Kwoba</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 7, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">AFTER A weeklong trial involving continuous attempts by federal prosecutors to avoid any political questions, four antiwar activists known as the St. Patrick's Four scored a major victory.<p>
The four--Peter DeMott, Clare Grady, Teresa Grady and Daniel Burns--were on trial for the symbolic act of pouring blood on the walls and entry hall of a military recruiting office in Ithaca, N.Y., on St. Patrick's Day in 2003. The Feds charged the activists with "conspiracy to impede an officer of the United States," which could have meant six years in prison and $250,000 in fines. But after hours of deliberations last week, a jury found the four not guilty.<p>
This was the first federal conspiracy charge against antiwar protesters since the Vietnam War, and the verdict represents a major blow against the criminalization of dissent.<p>
The four activists emerged from the trial emboldened. As Peter DeMott told <i>Socialist Worker</i>, "[Our victory] put the government on notice that principled dissent is the order of the day. And they're going to see more of it."<p>
The four were found guilty of two lesser misdemeanor charges, but the jury's decision to acquit on the conspiracy charge was a breakthrough--especially given the conditions under which the defense was presented. <p>
For example, activists say the trial was rife with collusion between prosecutors and the judge. Throughout questioning, the judge repeatedly sustained objections of prosecutors whenever the defendants attempted to speak of the illegality of the U.S. war on Iraq, international law and its relevance to their actions, the history of nonviolent civil resistance, and any facts, descriptions or experiences related to the war. <p>
"The closer we got to the truth of violence and war, the more the government tried to suppress the truth," said Peter. "I'm confident that if we could have explained our actions in the full context of the Constitution, international law and the Nuremburg principles...the jury would have acquitted us of all charges."<p>
The four are optimistic at the prospects for further resistance to the war. "When we see the government doing blatantly wrong, unjust and immoral things--such as killing over 100,000 Iraqis, nearly 2,000 of our own troops and spreading contamination from depleted uranium--it makes people aware of the need for resistance," said Peter. "They don't want people to speak out and act out." <p>
"But," Clare added, "in terms of resisting this war-making with the power of truth and persistence...anything is possible."<p>

