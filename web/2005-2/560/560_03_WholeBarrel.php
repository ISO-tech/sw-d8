<font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Scandals shake Republican power-brokers in Washington, but...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The whole barrel is rotten</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 7, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THE SCANDALS enveloping the White House and key Republican leaders in Congress--Rep. Tom DeLay and Sen. Bill Frist--are just the beginning.<p>
"Forget about all those details down in Texas that make your teeth hurt; don't bother to learn the difference between [DeLay's GOP fundraising outfits] Trmpac and Armpac," wrote <i>New York Times </i>columnist Frank Rich. "Fasten your seat belt instead for the roller coaster of other revelations and possible indictments that's about to roar through the Beltway."<p>
It's certainly a delight to see the thuggish DeLay shoved out of his role as House Majority Leader over violations of Texas campaign finance laws.<p>
But the fact is that DeLay didn't create today's staggeringly corrupt political culture. On the contrary, he's merely the leading practitioner of a corporate-controlled political system that's thoroughly bipartisan. For every example of Republican making the headlines, you can find a Democratic counterpart, even it they're outside the national spotlight.<p>
Take Senate Majority Leader Bill Frist's alleged insider trading of stock in the health care chain HCA, founded by his father and run by his brother. Such behavior smacks of Republican arrogance--so flagrant that investigators decided they had to act, though with the political cover provided by falling opinion polls about the performance of both the GOP Congress and George W. Bush.<p>
But is Frist, a gopher for the medical-industrial complex, really any more corrupt than former Sen. Tom Daschle of South Dakota?<p>
As majority and then minority leader of the Senate until he was voted out of office last year, Daschle diligently fronted for Citigroup, the financial giant that runs its credit-card operations out of Daschle's home state.<p>
While it was the Republican Congress that finally passed the so-called bankruptcy "reform" law earlier this year, it was Daschle who kept the proposal alive before that. His Democratic successor, Minority Leader Harry Reid, voted in favor, along with 17 other Democrats. Hillary Clinton ducked the vote--but supported a similarly restrictive bill in 2001.<p>
The new law, which will force poor and working-class people overwhelmed with debt to keep paying off creditors for decades, takes effect October 17--just in time to add to the misery facing survivors of Hurricane Katrina.<p>
While DeLay's ruthless political machine is fueled by an endless supply of corporate cash, the same is true of leading Democrats. Take former California Gov. Gray Davis--recalled in 2003 in an unprecedented special election--whose pay-to-play fundraising machine turned the Democratic-controlled state government into a profitable playland for big business. Most notoriously, Davis and his Democratic allies in the state legislature handed over the state's power grid to the likes of Enron, which bled California consumers and the state budget dry.<p>
More scandals are engulfing the Democratic establishment in Illinois, where federal prosecutors are indicting droves of operatives in Chicago Mayor Richard Daley's machine for selling jobs and funneling contracts to campaign donors. This can't be shrugged off as traditional Chicago backroom politics. Implicated is none other than Rep. Rahm Emmanuel, who runs the Democratic Congressional Campaign Committee for the 2006 elections.<p>
And remember that it was the Daley machine--in the person of the mayor's brother, William, Bill Clinton's secretary of commerce--who oversaw the passage of NAFTA in a Democratic-controlled Congress, a business-run operation that set the benchmark for the Bush-era handover of legislation for corporate lobbyists. <p>
The sleazy scandals of DeLay, Frist and Bush don't represent some aberration from the norms of U.S. politics. Their scams are bigger, more brazen and cross the legal lines more often. But it's essentially business as usual in Washington.<p>
The political impact on Bush is magnified, of course, by the growing discontent with the U.S. imperial slaughterhouse called Iraq, as well as the murderous abandonment of New Orleans and the scramble to profit from reconstruction. The mounting disgust over the scandals and the direction of U.S. politics can lay the basis for renewed opposition to the pro-business agenda supported by both parties.<p>

