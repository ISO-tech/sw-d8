<font face="Times New Roman, Times, serif" size="4">Ideologue in hot water over racist "solution" to crime</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bennett's bigotry</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 7, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">IF WILLIAM Bennett cares about "morality" as much as he claims to, he has a strange way of showing it.<p>
On a recent edition of "Morning in America," the Republican ideologue's weekly radio show, a caller weighed in with the opinion that the Social Security "crisis" is a result of "lost revenue from the people who have been aborted in the last 30 years." Bennett, the former Reagan administration education secretary better known asbeing the author of <i>The Book of Virtues</i>, dismissed the suggestion, saying that economic reasoning shouldn't come into play when talking about morals. <p>
After all, he added, if "you wanted to reduce crime...if that were your sole purpose, you could abort every Black baby in this country, and your crime rate would go down." To do so "would be an impossible, ridiculous and morally reprehensible thing to do. "But," he reiterated, "the crime rate would go down."<p>
The conservative pundit later told ABC News that he was extrapolating from the book <i>Freakonomics</i>, which suggests that falling crimes rates are related to increased abortion rates decades ago. But the book never makes the connection between race and crime that Bennett did.<p>
When the watchdog group Media Matters for America broke the story, even the Bush administration was forced to distance itself from Bennett, saying, "The president believes the comments were not appropriate." But to listen to Bennett and his conservative friends, he was the victim of a liberal smear campaign.<p>
In interviews after the incident, Bennett seemed to believe that the problem was that people thought he was saying Black babies <i>should</i> be aborted. But there's no doubt that Bennett was serious about his racist assertion that Blacks are responsible for the crime rate in the U.S.<p>
Bennett couldn't resist adding insult to injury--attacking the Congressional Black Congress. "When it comes to abortion, my wife's program, Best Friends, has kept more young women from having abortions...than the entire Black Caucus," Bennett told Fox News Channel's Sean Hannity. "She has done more for inner-city Black girls than the entire Black Caucus. So I will not bow my head to any of these people." <p>

