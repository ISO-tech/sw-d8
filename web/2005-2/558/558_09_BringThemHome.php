<font face="Times New Roman, Times, serif" size="5"><b>Chasing Bush from Texas to Washington</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 23, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">THREE BUSES are entering the last leg of their long journeys that took military family members, veterans and antiwar activists from Cindy Sheehan's Camp Casey in Crawford, Texas, to Washington, D.C.<p>
At more than 40 cities along the way, participants in the Bring Them Home Now tour got off their buses to speak at high school classes, college campuses, pickets, candlelight vigils and peace group meetings.<p>
At Northeastern Illinois University (NEIU) in Chicago on September 6, some 70 students and faculty turned out to hear Cody Camacho, a member of Iraq Veterans Against the War, and three members of Gold Star Families for Peace--Al Zappala and Juan Torres, whose sons were killed in Iraq, and Loretta Capeheart, an NEIU professor whose nephew was killed in Iraq.<p>
"It's not the rich kids that go to war, it's the working class kids who do," said Al Zappala, explaining that the military depends upon hardship to recruit people into its ranks.<p>
"This isn't just about Cindy Sheehan," said Camacho, who served in Iraq from 2003 to 2004. "There are 1,887 Gold Star families now, and we're all asking questions. Some of them aren't being answered, and some of them are being avoided. Congress had the voice to stop this war, and they choose to turn their head when I was sitting in Kuwait."<p>
All the speakers talked about how Camp Casey had inspired not just them, but thousands of others who passed through--and many millions around the country who were looking for someone to challenge Bush's occupation of Iraq.<p>
The bus also made a stop at Senn High School, where students and teachers continue to organize against a new Navy academy that has seized a wing of their school.<p>
In Columbus, Ohio, on September 8, nearly 100 people attended the tour event at the Columbus Mennonite Church. Following a community potluck that brought together activists and military families from around Central Ohio, members of Military Families Speak Out and Gold Star Families for Peace moved the audience with their stories--of pain and loss, but also hope and action.<p>
One MFSO member brought home the devastation of having a family member at war, describing the daily stress and strain overtaking every aspect of her life. Gold Star Families for Peace member Celeste Zappalla talked about the constant worry of hearing bad news, how "every knock on the door, every phone call, brought a sense of terror."<p>
Zappalla had stern words for the Bush administration and anyone else who said the war should continue in order to honor the slain soldiers. "We may be grieving mothers, but we're not stupid," Zappalla said.  <p>
One of the important aspects of the tour stop was that it encouraged local military families to share their stories and become active participants. Two of the speakers had only met the tour that day and joined them in meetings with the mayor of Columbus and state legislators.<p>
The tour has helped to make an important point--the vigil started August 6 by Cindy Sheehan in Crawford and turned into a national phenomenon by thousands of others continues on with the September 24 antiwar march in Washington. Then, it will be up to activists across the country to bring the movement forward in their cities as well.<p>
<i>Loretta Capeheart, David Hughley, Pranav Jani and Sam Jordan contributed to this report</i>.<p>

