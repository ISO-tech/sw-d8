<font face="Times New Roman, Times, serif" size="4"><b>Immigrants are welcome here</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Mike Corwin</b></font><p>
<font face="Times New Roman, Times, serif" size="3">AUSTIN, Texas--Some 500 people marched and rallied against the anti-immigrant Minutemen here September 17. During the spirited march to the state capitol, protesters chanted and carried signs saying "Minutemen AKA KKK" and "Minutemen are the illegals" and other placards denouncing the racism of the anti-immigrant right.<p>
The march, which brought together students and workers and people from across the state, was one in a series of statewide events and activities to counter the Texas Minutemen. The Minutemen plan to patrol the Texas-Mexico border in the El Paso area and harass day laborers in Houston.<p>
The city council in El Paso recently passed a resolution opposing the activities of the Minutemen.<p>
At the state capitol, a group of 30 Minutemen supporters had gathered, led by the infamous cable TV host Alex Jones. As the official rally began, the bigots, brandishing signs saying "Mexico out of U.S.A." attempted to march up to the rally space. A multiracial group gathered to confront them, chanting "Racists go home!" The police kept the two group separated, but after being yelled at for 10 minutes, the racists withered and retreated.<p>
Later, Jones, who combines paranoid rants about a "new world order" with anti-immigrant hysteria, tried to use a bullhorn to spew his garbage. He was immediately surrounded by a crowd of antiracists, who chanted and taunted him mercilessly until he slinked off.<p>
The day's events showed the breadth of opposition to the Minutemen and the willingness of people to confront racism.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Hurricane Katrina relief</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Keeanga-Yamahtta Taylor</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--Thousands of stranded New Orleans evacuees have come to the Chicago area in the three weeks since Hurricane Katrina destroyed their city.<p>
Evacuee Minor Wilson, who is staying with his brother outside of Chicago, spent two days trapped on the roof of his house. "The people here have been real nice, but they never should have done us like they did in New Orleans," he told <i>Socialist Worker. </i>"People treat their dogs better than they treated us. In New Orleans they just wanted to get rid of the Black people, and now they have done that--but I'm going back."<p>
There has been an outpouring of grassroots relief efforts to try to meet the needs of the evacuees. Churches around the city have been collecting goods, and hundreds of individuals have been signing up at the Chicago Park District's Fosco Fieldhouse to volunteer.<p>
There's also been an attempt to connect the plight of the evacuees to some of the larger political issues that groups have been organizing around. The local organizing committee of the Millions More Movement in Chicago organized a last-minute fundraiser called the Hurricane Katrina Relief-A-Thon. The fundraiser was a tremendous success as 150 people raised over $1,000 in a little over three hours. Local entertainers read poetry, sang songs and played instruments to raise money.<p>
-- At Hunter College and Columbia University in <b>New York City</b>, campus-wide coalitions met last week to discuss how to coordinate relief efforts at their campuses. On September 9, activists held a press conference and picket at the FEMA offices in New York. This was followed by a fundraiser for relief, organized by Palestinian groups, called "Refugees for Refugees."<p>

