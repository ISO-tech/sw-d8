<font face="Times New Roman, Times, serif" size="5"><b>"A great chance for antiwar activists to get organized"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Erik Wallenberg</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 23, 2005 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">BURLINGTON, Vt.--More than 250 people packed into the University of Vermont's (UVM) largest lecture hall September 9 to hear Dave Cline, president of Veterans for Peace, speak against the war in Iraq, his experience at Camp Casey in Crawford, Texas and later in Covington, La.<p>
This was the first event for the Will Miller Social Justice Lecture Series--in memory of Will who was a veteran for peace, a professor at UVM and a life-long activist--and kicked off the Vermont Says No to War conference which took place the following day. Dozens of organizations worked together to make the conference a success, including antiwar, women's rights, labor and environmental groups.<p>
The opening plenary included Joseph Turcotte from Iraq Veterans Against the War, who spoke about the lies that have been told by the government and media to justify the war and atrocities committed in Iraq. "My unit killed civilians, and they knew it," Turcotte said.<p>
Jerry Colby, president of the National Writers Union and member of U.S. Labor Against the War, said that al-Jazeera reporters are being targeted for assassination and that more journalists have been killed in this war than any other.<p>
The more than 20 workshops held throughout the day included, "Lessons of the Vietnam Antiwar Movement," a panel of "Military Families Speak Out," "Do You Feel a Draft" and "Free Palestine and Fight Anti-Arab Racism." The closing panel included a panel of international voices, including Nilda Medina-Diaz, a leader of the struggle to kick the U.S. Navy out of Vieques, Puerto Rico, and Krishna Ahooja Patel, president of the Women's International League for Peace and Freedom.<p>
Patel said that "the cost of the U.S. wars are paid by the people of the U.S." and argued that the lack of relief for those in need in New Orleans by the government exposes the same racism that is used to justify the occupation of Iraq. For his part, Cline called for the troops to get out of Iraq now, arguing "the people of Vietnam were never given one iota of justice from the U.S."<p>
In meeting on the September 24 antiwar protest set for Washington, Ahmed Shawki, editor of the <i>International Socialist Review, </i>highlighted this point, saying, "When the U.S. empire is strong, we are weak, and where we are strong, they are weak."<p>
Barb Prine, a member of Military Families Speak Out, added that "it feels like the tide has turned and there's opportunity for people to engage in antiwar work. This is a great chance to get organized."<p>

