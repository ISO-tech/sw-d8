<font face="Times New Roman, Times, serif" size="5"><b>Defeat for the border vigilantes</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jocelyn Blake</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 23, 2005 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">HUNDREDS OF people surrounded both sides of the border fence that separates Calexico, Calif., from Mexicali, Mexico, September 17 to stand up against the anti-immigrant vigilantes. <p>
Protesters on both sides pounded on the fence and shouted, "Queremos un mundo sin fronteras!" (We want a world without borders!). Sitting on top of the border wall, one protester held up an effigy of an anti-immigrant vigilante dressed in a pointed white hood.<p>
Some 300 protesters came to Calexico on the U.S. side to show the Minutemen and its offshoot groups that they aren't welcome in California. <p>
A group called Friends of the Border Patrol had set September 16 as a date for mobilizing a "citizen patrol" of the border near Calexico--after the model of the anti-immigrant Minutemen who descended on Arizona earlier this year. But the weekend passed with no signs of vigilante activity. Only signs that read, "Minutomen, your minute's up. Now go home," could be found in areas where the bigots had planned to assemble.<p>
In the weeks prior to the protest, Calexico city officials passed a unanimous resolution condemning the presence of any citizen patrol groups. "They are not needed here," said Calexico Mayor Alex Perrone. "If they come, I am going to tell these Minutemen and Friends of the Border Patrol, 'Go to New Orleans. Show you're patriotic and help your fellow man.'"<p>
Friends of the Border Patrol organizer Andy Ramirez told the <i>San Diego Union Tribune </i>that of 125 people trained for the patrol, only 20 to 30 would be participating--far short of the 2,000 that the group originally claimed would show up. "We have scaled back partially because of heat from (choosing) this weekend," said Ramirez--referring to his group's blatantly racist decision to begin its operation on Mexican Independence Day. But even the embarrassingly low 20 to 30 appeared to be a vast overestimation.<p>
This weekend marked another victory for immigrant rights activists in California, who have challenged the vigilantes, both at the border and at anti-immigrant demonstration in the Los Angeles area.<p>
The vigilantes have experienced another demoralizing defeat. But the other face of border racism--disastrous immigration policies like Operation Gatekeeper --must also be challenged. <p>
"These anti-terrorist policies, where every migrant is a potential terrorist, is feeding hate against migrants," a local resident said at the protest. "It doesn't stop with migrants--it will spread as hate against other people unless we say, 'Down with this fascist movement!'"<p>

