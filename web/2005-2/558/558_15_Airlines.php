<font face="Times New Roman, Times, serif" size="4">More union-busting through bankruptcy courts</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Airline unions hammered</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 23, 2005 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">THE BANKRUPTCY filings by airlines Delta and Northwest September 14 signal yet another wave of attacks on unions that have surrendered tens of thousands of jobs and billions of dollars in concessions since 2001.<p>
The carriers are expected to follow the example of United Airlines and US Airways in using Chapter 11 bankruptcy proceedings to slash costs--for example, dumping pensions into a government insurance fund, slashing jobs and ramming through wage and benefit cuts.<p>
US Airways has cut the number of jobs from 44,684 in 2001 to 22,460 today--and will soon merge with America West to create a low-cost, low-wage carrier. United has used bankruptcy to eliminate 42,253 jobs since 2001, about 44 percent of the total, as the company grabbed $3.5 billion in labor concessions.<p>
Now Delta and Northwest--which already obtained givebacks in order to avoid bankruptcy--are prepared to use the courts to accelerate their own cost cutting. "The big thing you can do in Chapter 11 is stiff your creditors and get rid of all the airplanes you don't want and force employees to accept givebacks," said Michael Roach, former president of America West airlines and now an industry consultant.<p>
While high fuel costs were blamed for triggering the bankruptcies, the underlying cause is the irrational economics of industry deregulation and the overcapacity in the industry that developed in the late 1990s.<p>
For Northwest, bankruptcy is part of a divide-and-conquer strategy in its efforts to defeat a strike by mechanics and extract more concessions from other workers who have endured previous cuts. When 4,400 members of the Aircraft Mechanics Fraternal Association (AMFA) walked out August 20 over demands to cut pay and eliminate half their jobs, members of other unions crossed their picket lines.<p>
Northwest used fear of replacement to bully members of the Professional Flight Attendants Association--from which it wants to extract $143 million and the elimination of half their jobs--into crossing AMFA's picket lines.<p>
For members of the Air Line Pilots Association--who have already agreed to $265 million in concessions--the enticement to scab was management's promise to avoid bankruptcy and protect pilots' pensions, something the company is now likely to pursue. Ramp workers, members of the International Association of Machinists (IAM), the union that the mechanics ousted in 1998, even took over some of AMFA's work--even though the company is demanding $107 million in givebacks.<p>
Yet there's no sign that Northwest's bankruptcy proceeding will lead to solidarity with AMFA strikers. Instead, the company is trying to shut the mechanics' union out of bankruptcy proceedings, claiming that they no longer have a contract and have "no forum" in court. AMFA, which is not affiliated with the AFL-CIO, remains isolated in the labor movement, with the important exception of a donation of $880,000 by the United Auto Workers.<p>
At Delta, management has to contend with only one major unionized group of workers--the pilots--and will use bankruptcy to club them into making concessions. "Pilots will have to do it or learn how to say 'Welcome to Wal-Mart,'" Mike Boyd, an airline industry consultant, told a reporter.<p>
In fact, by repeatedly accepting concessions to try restore profitability to the airlines, unions in the industry have only accelerated a vicious cycle in which unions are played off against one another within airlines, and then between them, as management at one company demands the same givebacks obtained by its rivals.<p>
This dynamic will continue until the airline unions join together to challenge the chaotic scramble for profits in a deregulated industry and demand a rational system of air transportation with decent jobs.<p>

