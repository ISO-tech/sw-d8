<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bush to the poor: Let them eat guns</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 23, 2005 | Page 13</font><p>

<font face="Times New Roman, Times, serif" size="3">IN THE late 1800s, a series of famines ravaged India, killing tens of millions. They were triggered by droughts and bad harvests, but the real cause was man-made.<p>
Under British rule, land formerly devoted to food crops were now devoted to export crops like cotton, and the traditional village system for holding grain in reserve for hard times had been destroyed. The problem wasn't shortages--merchants continued to export wheat in large quantities from India during the famines.<p>
Peasants, already living at the edge of destitution, could not afford to buy grain whose price had shot too high. But Viceroy Lord Lytton, like many British officials in his day, denounced "humanitarian hysterics," insisting that there must be no "interference of any kind on the part of government with the object of reducing the price of food."<p>
In 1877, British Famine Commission clarified the reasoning behind Lytton's Laissez-Faire attitude to famine: "The doctrine that in time of famine the poor are entitled to demand relief...would probably lead to the doctrine that they are entitled to such relief at all times."<p>
Though clearly the scale of death and disaster surrounding Hurricane Katrina doesn't compare to the Indian droughts and famines of the late 19th century, there are important parallels.<p>
First, there were the environmental and social conditions existing before the crisis that created a man-made disaster and made the natural disaster worse. Environmental degradation in the form of global warming has made storms potentially more virulent and, therefore, more dangerous.<p>
The destruction of wetlands that once acted as spillways and buffers for storm surges also added to the storm's destructiveness. Neoliberal capitalist policy in Washington and at the state level has drastically cut social spending, and in this case, particularly spending necessary to shore up the levees that protect New Orleans from flooding.<p>
Then there was the element of race and class. The evacuation plan was a non-evacuation plan for the mostly (but not exclusively) Black poor.<p>
But even for those workers who could afford to leave, many have no jobs and no flood insurance. Many will be forced to live in giant trailer parks indefinitely.<p>
Then there was the attitude to "looting." Looting was a sensible act by desperate people to get out and remove valuable food and supplies from stores before the flooding got too bad, or to obtain valuables that might be traded for passage out of the city. Yet the press and officials portrayed it as chaotic, animalistic behavior that justified a massive military and police crackdown.<p>
The seriousness of the state's intention to criminalize this basic act of self-preservation was exemplified by the arrest of a 73-year-old grandmother for allegedly taking $63 worth of food from a supermarket a day or two after the storm hit.<p>
The most striking parallel with British rule in India is the laissez-faire attitude to relief, best expressed by the former head of FEMA, Joseph Allbaugh, who briefly ran it for Bush starting in 2001. He denounced FEMA disaster mitigation programs as "an oversized entitlement program." FEMA was gutted accordingly.<p>
To paraphrase the British Famine Commission: The doctrine that in time of disaster the poor are entitled to survive by looting stores would probably lead to the doctrine that they are entitled to live free of hunger and poverty all times.<p>
The second prong of this neoliberal approach, seen hitherto only overseas, has been the increasing militarization of society, and a corresponding ideology that plays up military (remember the "humanitarian" invasion of Somalia) rather than civilian mechanisms to solve problems.<p>
Perhaps this whole thing was an exercise to acclimate Americans to the idea that the military can routinely occupy our cities. This could explain the official lack of enthusiasm for sending timely aid and rescue, and, on the other hand, the gusto and enthusiasm for turning New Orleans into an armed camp.<p>
The hungry, dying people in the Superdome needed food and water. Instead they got armored personnel carriers.<p>

