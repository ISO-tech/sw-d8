<font face="Times New Roman, Times, serif" size="5"><b>Trying to bury their greedy secrets</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Susan Dwyer</b><font face="Arial, Helvetica, sans-serif" size="2"> | September 23, 2005 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>The Constant Gardener,</b></i> directed by Fernando Meirelles, starring Ralph Fiennes and Rachel Weisz.</font><p>

<font face="Times New Roman, Times, serif" size="3">WHILE WAR and hurricane relief have captured most people's attention, Fernando Meirelles' new movie, <i>The Constant Gardener,</i> reminds us that AIDS continues its devastation.<p>
<i>The Constant Gardener</i>--adapted from John le Carr&eacute;'s novel of the same name--is set in Kenya, where more than 2.5 million people carry the virus. The film shows us people struggling against ignorance and massive corruption.<p>
Although the Kenyan AIDS crisis the backdrop of film, its real theme is more sinister. An international drug company has turned AIDS victims into uninformed guinea pigs, using them to test a frequently lethal drug that has nothing to do with their illness. The justification for this is offered by Marcus Lorbeer, played by Pete Postlethwaite, who says, "We're not killing people who wouldn't be dead anyway."<p>
Ralph Fiennes plays Justin Quayle, a terminally blinkered diplomat in the employ of the British Foreign Office. He meets and falls in lust with Tessa, well played by Rachel Weisz. Tessa is an activist, whose credentials are established when she asks tough questions about the Iraq war at a lecture delivered by Quayle.<p>
Soon after his affair with Tessa begins, Quayle is posted to Kenya. Tessa shows up at his apartment and demands that he take her with him. She offers a few options: She will go as his friend, his mistress or his wife. Since this is a tragedy, Quayle chooses wife.<p>
Up to this point, Quayle had been constant to nothing, except his love for cultivated plants. He loves Tessa, but since, unlike the plants, she is a sentient being, he is never sure why she married him. He knows that she is immersed in solving the AIDS crisis, working with a doctor who treats the people of Kenya's slums and villages, but he is too reticent and polite to meddle in her life. He wonders what she's up to with the Kenyan doctor, wonders at their close friendship, but demurs and spends his time instead "dreaming of a world without weeds."<p>
Tessa and her doctor are soon killed. This is not a spoiler. <i>The Constant Gardener </i>is not about the fact of the murders, or even about who the killers are. It's about what Tessa and her friend the doctor knew, why it killed them, and Quayle's search for the answers to those questions.<p>
He soon realizes the links between corporate greed, racism, international capitalism and governments, foreign and domestic. The film moves back and forth between past and present, showing the facts ahead of Quayle's understanding.<p>
Unfortunately, Meirelles uses the annoying jerky, hand-held cameras and jump cuts typical of action movies. Quayle, like Jason Bourne of the spy thriller movies, is remarkable for his ability to move relentlessly forward in threatening situations. But he grows as a character through his constancy and understanding, not from his seeming ability to twirl around in front of the Foreign Office.<p>
<i>The Constant Gardener</i> suffers from other problems that stem from the book. To be a diplomat in the service of an imperialist power is neither noble nor forgivable. At their first meeting, Tessa rightly calls Quayle a hypocrite and an apologist for war. A few hours later, she's in bed with him. And why, if Tessa can afford a house in Chelsea, does she need Quayle to take her to Africa?<p>
Never mind. Read le Carr&eacute;'s excellent and angry book for more details about the drug companies. And see the movie because, taken as a whole, it's good.<p>
It's worth it to hear these lines from a diplomat played by Danny Huston that seem to sum up the world of George Bush and Tony Blair: "Stop bleeding for bloody Africa and show some loyalty."<p>

