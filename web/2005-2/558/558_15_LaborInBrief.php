<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">September 23, 2005 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>AFL-CIO hurricane relief efforts</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THE AFL-CIO has established workers' centers in six Southern cities to provide assistance to Hurricane Katrina evacuees and help them organize. Co-sponsored by affiliated unions and state labor federations, the centers are located in Atlanta; Baton Rouge, La.; Dallas; Houston; Mobile, Ala.; San Antonio, Texas; and Pearl, Miss., near the heavily damaged town of Gulfport. <p>
According to Josh Cazares, a community liaison official with the AFL-CIO stationed in Pearl, the centers help "provide information about how to obtain help from FEMA, file for unemployment and access resources through the United Way, the Red Cross or social services." In addition, the centers--staffed by 1,000 volunteers from the AFL-CIO and its affiliates--help coordinate the distribution of water and nonperishable foods.<p>
"There is a lot of need for school supplies and work clothes," Cazares told <i>Socialist Worker</i>. "Those are the kind of things we are going to be coordinating."<p>
Workers centers have been used in recent years in nontraditional organizing efforts aimed primarily at immigrant workers in cities across the U.S., often outside existing union structures. They aim to provide workers with a support network by informing them of their rights, helping them to obtain social services and assisting organizing efforts.<p>
In the aftermath of Katrina, the AFL-CIO is following that example to raise the profile of organized labor in a part of the country historically hostile to unions, and build relationships that will help organizing efforts in the future.<p>
According to Cazares, one of the main supporters of the workers' center in Pearl is the United Auto Workers, which is undertaking a high-profile organizing effort at the big Nissan plant in the town of Canton. "We are getting calls from teachers for help purchasing school supplies," he said. "We need money to purchase things."<p>

<i>The AFL-CIO is seeking donations to the Union Community Hurricane Relief Fund. Donations can be made online at <a href="http://www.aflcio.org" target="_blank">www.aflcio.org</a> or by sending checks to: Union Community Fund Hurricane Relief Fund, P.O. Box 27306, Washington, DC 20038</i>.<p>

<font face="Times New Roman, Times, serif" size="4"><b>UNITE HERE</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><p>

<font face="Times New Roman, Times, serif" size="3">UNITE HERE announced its pullout from the AFL-CIO September 13 on the eve of a meeting that's billed as the "founding convention" of the rival Change to Win (CTW) coalition. The long-expected move by UNITE HERE brings the total number of unions that have abandoned the AFL-CIO to four, accounting for four million of what had been 13 million members.<p>
With 450,000 members, UNITE HERE is considerably smaller than the three unions that pulled out earlier--the 1.6 million member Service Employees International Union, the United Food and Commercial Workers, which has the 1.4 million, and the Teamsters, whose ranks also include1.4 million workers. But the disaffiliation nevertheless will deprive the AFL-CIO of $3.2 million in annual dues, bringing the total annual loss to the federation of $29 million--about a third of its budget.<p>
Another complicating factor is that UNITE HERE controls the Amalgamated Bank, where many unions have substantial funds.<p>
Meanwhile, the CTW unions are continuing their strategy of paying dues into state and local federations, attempting to retain their influence in local labor movements while starving the national AFL-CIO bureaucracy into submission. For his part, AFL-CIO President John Sweeney has, for now, backed off plans to retaliate by expelling the breakaway unions at all levels, setting aside a series of bureaucratic weapons approved at the AFL-CIO convention in July.<p>
The CTW meeting, set for September 27, is just a one-day affair--more of a rally to endorse the decisions made by top union leaders than a serious deliberation by union delegates. Among the rest of the seven-member CTW coalition, two unions--the Laborers and the Farmworkers--remain in the AFL-CIO. The other, the Carpenters, left in 2001.<p>
As with the rest of the unions in the Change to Win Coalition, UNITE HERE's decision to disaffiliate was taken by union leaders over the heads of the members.<p>
The further splintering of the U.S. labor movement comes as Corporate America ratchets up the pressure on labor by provoking strikes at Boeing and Northwest and threatening to slash pensions and health care at General Motors and parts maker Delphi. While both labor factions claim to be gearing up to organize the unorganized, neither side has yet put forward a strategy to resist the relentless demands for concessions.<p>

