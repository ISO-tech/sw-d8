<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 13, 2003 | Page 11</font></p>

<P><p><font face="Times New Roman, Times, serif" size="4"><b>Waukesha Engines</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Kevin Todd Prosen</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">WAUKESHA, Wis.--Members of the International Association of Machinists (IAM) Local 1377 struck May 2 following two months of contract negotiations with Waukesha Engines, a division of Dresser, Inc., a subsidiary of Halliburton. On May 1, workers voted 414-10 in favor of striking.</P>
<P>The company is demanding $5 million in concessions from the union--including a 20 percent increase in health insurance co-payments. Retirees, who average $400 a month in pension money, would be forced to pay as much as $6,000 dollars a year for health care. Since the strike began, they've had their health benefits cancelled by the company. </P>
<P>The company is pushing to eliminate consideration for seniority during layoffs of up to six weeks--which would allow the company to hire back experienced employees at lower wages--and is also pushing for a permanent two-tier wage system. "This is by no means an economic strike," Don Griffin, committee chairman of Local 1377, told <I>Socialist Worker</I>. "It's a strike for survival."</P>
<P>Even before serious negotiations were taking place, the company placed ads for replacement workers in the local newspapers and brought scabs in to train on the job. Steve Lamb, president of Dresser Inc., was brought in improve Waukesha Engines' "productivity"--by making workers pay. Lamb, of course, receives a fat salary of $1.6 million a year and unlimited health care. </P>
<P>"When they talk about improving productivity, they really mean taking it out on the workers," said Griffin. "It's all corporate greed. This company is so top-heavy, making some cuts higher up would mean immediate profit increases, but they don't want to hear that. We're getting fed up with workers being sacrificial lambs for the corporate office," he said.</P>
<P>With Tyson chicken workers involved in a bitter strike in nearby Jefferson, Wis., and local ironworkers and employees of the Waukesha transit system now also threatening strikes, a new mood of resistance is in the air. </P>
<P>Tentative plans are underway for a late June rally of striking workers at the state capital. This is exactly the kind of solidarity necessary to win the struggle against corporate greed and exploitation.</P>

<p><font face="Times New Roman, Times, serif" size="4"><b>General Electric</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Annie Levin</b></font></P>

<P<font face="Times New Roman, Times, serif" size="3">LYNN, Mass.--On June 7, 2,500 union members and supporters rallied in the pouring rain outside the plant gates of General Electric (GE). The rally was organized by International Union of Electrical Workers-Communications Workers of America (IUE-CWA) Local 201 and took place during a 24-hour strike at the plant--part of IUE-CWA's 2003 contract fight for its 16,000 members around the country.</P>
<P>In a tremendous show of solidarity, the rally drew a strong turnout from the Lynn plant and other local unions--as well as hundreds of GE workers who traveled by bus from places like Arizona, Kentucky, Kansas and Ohio. Sixty workers came from a non-union GE plant in Auburn, Maine, where people have been harassed and fired for trying to organize.</P>
<P>The union is fighting for a major increase in pensions, a freeze on health care costs for workers and retirees, an end to the outsourcing of union work, and protection for non-union workers' organizing rights. Union leaders have said that if the company fails to meet these demands, workers are prepared to strike on June 26.</P>
<P>At the rally, Lyn Meza, the retired vice president of Local 201, told <I>Socialist Worker </I>about the national significance of this fight. "GE's attack on the unionized workers in the U.S. is part of the attack of the major corporations and U.S. government on the working people at home and abroad that has been intensified since Bush declared an unending war on the people of the world," said Meza. "I think it's very important that the union workers in Lynn, with the support of their brothers and sisters from around the country, are here to stand up to corporate greed."</P>

