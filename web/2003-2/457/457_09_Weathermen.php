<P><font face="Times New Roman, Times, serif" size="4">Rise and fall of the Weather Underground</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Which way the wind blew</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Brian Jones</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 13, 2003 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">DOCUMENTARY<B><I>: The Weather Underground: '60's Radicals Revisit a Tumultuous Era, </B></I>directed by Sam Green and Bill Siegel. Now at New York City's Film Forum, to be followed by a national run in July.</font></P>
<I>
<P><font face="Times New Roman, Times, serif" size="3">THE WEATHER Underground</I> is a fascinating documentary that retraces the rise and fall of the Weathermen, a revolutionary terrorist group founded in the late 1960s that ultimately never developed anything close to a viable strategy for revolution.</P>
<P>"You don't need a weatherman to know which way the wind blows." Bob Dylan sang these lyrics to a generation around the world that was radicalized by the Vietnam War.</P>
<P>In the U.S., the largest antiwar organization, the Students for a Democratic Society (SDS), had 100,000 members. "It seemed to me we were entering an incredible period of revolution, and I didn't want to miss it," recounts former Weathermen member Naomi Jaffe in the film. "I wanted to be a part of it."</P>
<P>Tragically, the revolutionary left at that time was dominated by Maoism--which, wrongly viewing U.S. workers as "bought-off," saw revolution as guerilla-style uprisings of the poorest and most oppressed. In 1969, a group of radicals broke away from SDS to prepare themselves for this revolution, challenging each other to be "more violent, and therefore more revolutionary."</P>
<P>That year, the Weathermen put out a call to "bring the war home" by rising up in "days of rage" in Chicago. When only about 150 people showed up, reinforcing the idea that the masses weren't ready for revolution, they went on a rampage, smashing store windows and street-fighting with the police.</P>
<P>The group was clearly isolated, but armed with a feeling of moral superiority, it remained determined to fight in "concert with the people of the third world," even if ordinary Americans wouldn't fight with them.</P>
<P>In response to the police murder of Black Panther leader Fred Hampton, the Weathermen planned a terrorist attack on a military officers' dance, but the bomb detonated prematurely and killed three Weathermen instead. Soon after, the group was driven underground by intense FBI harassment.</P>
<P>By the mid-1970s, the Vietnam War was over, the radical movements at an ebb, and the Weathermen began to feel it was pointless to remain underground. By the late 1970s, most had turned themselves in.</P>
<P>The strength of <I>The Weather Underground</I> is that it's sympathetic to the Weathermen for all of the right reasons, and critical of them for most of the right reasons. Many former members express mixed feelings about their pasts.</P>
<P>Mark Rudd admits he has a hard time "teasing out what was right from what was wrong," and Brian Flanagan, now a bar owner in New York City, concludes, "I think that the Vietnam War made us all crazy."</P>
<P>Most still believe in the need for revolutionary change--but admit that they have little idea how to make it happen. <I>Weather</I> <I>Underground </I>provides a vivid window into a time when millions of people considered themselves revolutionaries--and shows the tragic consequences when movements are armed with bad political strategies.</P>



