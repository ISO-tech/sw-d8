<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Stoking fear to attack immigrants</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 13, 2003 | Page 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"WE MAKE no apologies," Attorney General John Ashcroft told Congress last week, dismissing critics of the Justice Department's sweeping detention of 762 Arab and Muslim immigrants during the months after September 11. But even the Justice Department's inspector general has issued a report documenting the systematic mistreatment of the 762 detainees, most of whom were rounded up on spurious evidence--detained after nothing more than random traffic stops or anonymous tips that they were Muslims who kept "erratic" schedules.</P>
<P>The report also found that many of the detainees faced a "pattern of physical and verbal abuse," in which they were routinely slammed against walls before taping statements, and "unduly harsh detention policies," including 23-hour-a-day lockdown. Most were held for months in a "communication blackout," without access to legal representation or contact with their own families--before they were deported for alleged immigration violations.</P>
<P>In the end, not one of the 762 was charged with terrorism.</P>
<P>The inspector general's report of the post-September 11 detentions offers a glimpse of the nightmare confronting Arab and Muslim immigrants in the U.S. today.<B> </B>The number of Pakistanis, Jordanians, Lebanese and Moroccans deported has doubled over the last two years, while the number of Egyptians has tripled.</P>
<P>And an even larger wave of deportations is just starting. Beginning last November, the Justice Department began requiring that men above the age of 16 from 26 countries--all, except for North Korea, majority-Muslim countries, which Ashcroft called "breeding grounds for terrorism"--register with immigration authorities.</P>
<P>More than 82,000 men have registered with U.S. immigration authorities so far. To date, more than 13,000 of those who have registered--roughly 16 percent--face deportation for alleged immigration violations.</P>
<P>Immigration violations can be as minor as a recently expired visa or missing by a day or two the government's requirement to notify authorities within 10 days of moving to a new address. And many are "illegal immigrants" only because they have been waiting years for the U.S. government to work its way through its own backlog to finish processing their applications for legal status. According to Mark Engler and Saurav Sarkar of the <I>Progressive </I>magazine, immigration lawyers estimated that 95 percent of 500 California men detained last December by the Immigration and Naturalization Service had pending applications for legal residency. </P>
<P>Detainees held on immigration charges since 9/11 have no right to court-appointed legal counsel, however, and their families can only get lawyers if they can pay for them. In the last two years, deportations have ripped thousands of immigrant families apart and destroyed entire communities--all under the guise of "fighting terrorism."</P>
<P>Farouk Ali-Haimoud, aged 22, was recently acquitted of all terrorism charges. But he has spent most of the last 20 months behind bars.</P>
<P>First he was detained for several months in the post 9/11 roundup--after federal agents raided his apartment looking for a previous resident and noticed "suspicious" evidence. In April 2002, he was arrested and charged, along with three other Muslims, with operating a terrorist cell outside Detroit.</P>
<P>Ali-Haimoud was held in an isolation cell 23 hours a day for 14 months before he was acquitted on June 5. His mother sold furniture to help pay for legal fees and lost her job during the trial. It is hard to conclude that "justice" prevailed for Ali-Haimoud.</P>
<P>In March, the Department of Homeland Security declared that all immigrants applying for political asylum in the U.S. will automatically be imprisoned for the months or years needed to process their applications. In May, the Supreme Court let stand a case allowing the Justice Department to continue to hold secret deportation hearings for immigrants suspected of terrorist ties.</P>
<P>Ashcroft spoke the truth last week when he informed the House Judiciary Committee, "We did not violate the law." But he didn't mention that he has personally rewritten the law, to regard an entire race of immigrants as guilty until proven innocent.</P>
<P>"The war on terror," said Anthony Romero, executive director of the American Civil Liberties Union, "quickly turned into a war on immigrants." And that war is escalating.</P>



