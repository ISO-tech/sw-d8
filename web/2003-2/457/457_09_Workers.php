<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Can U.S. workers change society?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 13, 2003 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE U.S. working class has a long and rich tradition of struggle--one we're not taught in the history books at school. Take the Great Railroad Rebellion of 1877, when hundreds of thousands of railroad workers struck, battling police and militias from one end of the country to the next--culminating in a general strike in St. Louis.</P>
<P>It is to this fight that we owe the existence of those large armory buildings in virtually every major city in the country. They were built to help put down any future working-class rebellions.</P>
<P>Some other upheavals: The Pullman strike of 1894 (The Debs Rebellion); the mass strikes for the eight-hour day in the 1880s and against the execution of the Haymarket martyrs; the New Orleans General strike of 1892, in which Black and white workers united in solidarity and won; the IWW Free Speech fights in the early 1900s; the Seattle General Strike of 1919.</P>
<P>And then of course there were the mass strikes of the 1930s--from the San Francisco General Strike to the mass sitdowns in the auto industry--that finally brought mass industrial unionism to this country, followed by a mass wave of postwar strikes. These are just a sampling of the tremendous class battles that have been fought throughout U.S. history.</P>
<P>But the class struggle is a very contradictory thing. Long periods of calm are interrupted by huge explosions of struggle.</P>
<P>The "calm" periods are periods of ruling-class assault on workers' organizations, working conditions and living standards. That explains what follows--the mass explosions of anger and bitterness that have been described by one historian as "bargaining by riot."</P>
<P>Each period of defeat is accompanied by funeral hymns about the end of the class struggle and the irrelevancy of class. The result is that throughout the labor movement's history there have been periodic breaks in the organizational and political continuity of the movement.</P>
<P>Each new wave of struggle has not had the benefit of learning from the experiences of previous waves. "The American worker is very combative," writes Trotsky. "They have had the most rebellious strikes in the world. What the American worker misses is a spirit of generalization, or analysis, of his class position in society as a whole."</P>
<P>The breaks in political continuity in the U.S. labor movement is clear when we look at May Day and International Women's Day, two of the socialist movement's biggest holidays. The irony is that both originated in the U.S.</P>
<P>The first May Day was marked in 1886 by a mass strike wave in the U.S. demanding the eight-hour day. It was the (later very conservative) American Federation of Labor (AFL) that first urged May Day to become an international workers' holiday. And it was a strike of more than 20,000 New York City garment workers in 1909 that inspired the creation of International Women's Day. </P>
<P>Take this statement, written in 1881: "A struggle is going on in the nations of the civilized world between the oppressors and the oppressed of all countries, a struggle between capital and labor..." This is in the preamble to the AFL's founding statement. Yet decades later, the vast majority of U.S. workers have never even heard of May Day.</P>
<P>Workers in the U.S. have already changed society--though many of those changes are under attack. Social Security, Medicare, the shorter workday, the right to organize a union, the right to strike--there are many battles that, as a result of the decline of the labor movement, workers will have to re-fight and win all over again.</P>
<P>The scale of the employers' attack means that a new phase of labor upheaval is not far off. Let's hope that this time we'll be able to build a movement where the instinctive militancy of American workers is combined with that "spirit of generalization" that will be able to pose a real challenge to the disaster that is capitalism. </P>



