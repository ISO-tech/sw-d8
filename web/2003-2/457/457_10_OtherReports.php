<p><a name="LEIU"></a><font face="Times New Roman, Times, serif" size="4"><b>Fight police repression</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Maryanne Zarrella</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEATTLE--Police used pepper spray, concussion grenades and rubber bullets to disperse a peaceful demonstration last week during the Law Enforcement Intelligence Unit (LEIU) conference, a gathering of intelligence agents from police departments across the country. This year's conference marks the first time that federal intelligence personnel have participated. </P>
<P>More than 700 demonstrators attended a rally in Westlake Park organized by the LEIU Welcoming Committee to protest the conference. Homeland Security czar Tom Ridge was scheduled to address the conference, which featured such topics as "International Terrorism," "Protecting our Borders" and "Domestic Terrorism: Left and Right."</P>
<P>In 1993, the San Francisco Chronicle revealed that local police secretly used the LEIU to preserve intelligence files that were supposed to be destroyed. The crowd began a nonviolent, permitted march toward the LEIU conference site where dozens of police waited in riot gear.</P>
<P>There, the police attacked the crowd without warning. Demonstrators were pepper-sprayed and shot with pellets as they were returning to the rally site to disband. </P>
<P>At least one undercover officer was filmed throwing punches before leaping to the safety of the police line when teargas was sprayed. Capt. Mike Sanford claims that police moved to disperse the crowd after being pelted with mason jars, ball bearings and other projectiles. </P>
<P>Yet police did not respond to a request from the Seattle Post-Intelligencer to display the projectiles that were allegedly thrown at them. A dozen protesters were arrested, and many suffered cuts, welts and contusions. Four were taken to the hospital. </P>
<P>The day after the attack, activists gathered outside City Hall to protest the police violence. We have to hold the police accountable for their actions--and build a movement that draws connections between the war abroad and the war on civil liberties at home.</P>


