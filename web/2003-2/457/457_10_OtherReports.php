<p><a name="NYC"></a><font face="Times New Roman, Times, serif" size="4"><b>New York City housing protest</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jessica Carmona-Baez</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--More than 200 people gathered June 1 in Union Square to demand stronger rent laws and a moratorium on rent increases. The rent laws that currently protect tenants are set to expire June 15 at midnight. </P>
<P>If they are renewed as is, rent regulations will slowly be killed. This would give a green light to landlords to raise rents to astronomical levels at a time when job security is fading and the price of public transportation just increased.</P>
<P>Protesters wore hats and T-shirts with the slogan "Stronger rent laws now!" and passed out leaflets with information about the crisis in rent control. Even under the current laws, the number of homeless people is on the rise.</P>
<P>Several city council members addressed the crowd, whose anger was evident. "New York City is becoming uninhabitable," said one protester. "They are on a campaign to push people out of New York!"</P>

