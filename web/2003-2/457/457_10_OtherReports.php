<P><p><font face="Times New Roman, Times, serif" size="4"><b>Repeal the Rockefeller drug laws</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Wrigley-Field and Andrew Jagunich</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Thousands gathered June 4 in City Hall Park to oppose the state's Rockefeller Drug Laws, among the harshest in the country. New York State prisons house 18,300 people on drug charges. An astonishing 94 percent of these prisoners are Black or Latino, even though government studies show that the majority of drug users and sellers are white.</P>
<P>One woman at the protest said that her son faces 15-years-to-life in prison if he is convicted in a pending drug case. That's the mandatory sentence in New York State for selling two ounces or possessing four ounces of drugs.</P>
<P>Another protester came to the rally partly because his brother recently finished a drug sentence. He brought with him about half the students in the eighth grade class he teaches because, as he explained, "this is something that affects their communities. The Rockefeller Drug Laws are like the California three strikes laws--they target poor communities and communities of color."</P>
<P>At a time when New York is slashing social spending to compensate for a massive budget deficit, repeal of the Rockefeller laws would save the state $245 million, according to Doug Greene of the Drop the Rock Coalition. Randy Credico, from Mothers of the Disappeared, made an analogy to earlier antiracist struggles. "We didn't fight to reform slavery," said Credico. "We fought to repeal it."</P>

