<p><a name="Kiss"></a><font face="Times New Roman, Times, serif" size="4"><b>Protest Kissinger</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Lucy Herschel</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--About 40 activists rallied June 3 in the rain in front of Lincoln Center to protest the presentation of an award to Venezuelan billionaire Gustavo Cisneros by U.S. war criminal Henry Kissinger. Cisneros is the wealthiest man in Venezuela and owner of a majority of Latin America's telecommunications and media.</P>
<P>Cisneros was implicated in the failed April 11, 2002, coup against democratically elected president Hugo Chavez. "It's very alarming to see the connection between Henry Kissinger, the war criminal, and the wealthiest man in Venezuela," Omar Sierra, of the Venezuela Solidarity Committee, told Socialist Worker. "There is a clear connection here between the coup-backers in Venezuela, and some very right-wing characters in the United States."</P>

