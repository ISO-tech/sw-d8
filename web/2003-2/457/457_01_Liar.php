<font face="Times New Roman, Times, serif" size="5"><b>Liar!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 13, 2003 | Page 1</font></p>

<P><font face="Times New Roman, Times, serif" size="3">AIR FORCE One soared 31,000 feet above the conquered country of Iraq last week, and George W. Bush, returning from his Middle East tour, peered out the window, pointing out landmarks.</P>
<P>Maybe the Liar-in-Chief was also hoping to see some "weapons of mass destruction." Because nearly two months after the end of the U.S. war on Iraq--a war that was sold to the world as a pre-emptive strike against a rogue state with a deadly arsenal--the U.S. has yet to find a trace of that arsenal.</P>
<P>In Britain, Prime Minister Tony Blair--or "Bliar," as the <I>Economist </I>magazine nicknamed him--is catching heat for the "dodgy dossier" that he presented in September to "prove" Saddam Hussein could deploy weapons of mass destruction in as little as "45 minutes." Not only opponents of the war, but officials of Britain's intelligence agency MI6 say that the evidence was cooked.</P>
<P>Similar complaints can now be heard from U.S. spymasters. In May, CIA analysts calling themselves Veteran Intelligence Professionals for Sanity wrote to Bush, describing the administration's case for war as "a policy and intelligence fiasco of monumental proportions." The Bush team "cherry-picked the intelligence stream" to portray Iraq as a threat when it was nothing of the sort, Patrick Lang, a former head of the Defense Intelligence Agency, told the <I>Los Angeles Times.</P>
</I><P>Some CIA officials say that they were being muscled around by the Bush administration--in the person of Dick Cheney, a frequent visitor to the agency's headquarters in the run-up to the war--to produce evidence linking Iraq to the September 11 attacks. And when the spies didn't provide the information they wanted, the Bush gang turned to Donald Rumsfeld's personal intelligence agency--the Defense Department's brand-new Office of Special Plans--to fill in the blanks.</P>
<P>In February, Colin Powell was reading through the "evidence" in preparation for his speech to the United Nations when he reportedly tossed several pages into the air. "I'm not reading this. This is bullshit," Powell said, according to <I>U.S. News and World Report.</I> The secretary of state apparently told British Foreign Secretary Jack Straw that he hoped the evidence wouldn't "blow up in their faces," <I>U.S. News </I>reported.</P>
<P>If the Bush gang really wanted to find weapons of mass destruction in Iraq, many are still in plain sight. Like the estimated 10,000 cluster bombs and bomblets that lay unexploded across the country, waiting to take more innocent lives.</P>
<P>Then there are the many victims of Washington's weapons of mass destruction. Like the thousands of children suffering from cholera, dysentery, typhoid and other diseases caused by the lack of clean water after U.S. bombs--and a decade of economic sanctions--left Iraq's infrastructure in a shambles.</P>
<P>The U.S. invasion of Iraq was a bloody and brutal war, led by a liar who eagerly traded blood--mostly Iraqi blood--for oil. We have to expose the truth about Washington's war makers--and oppose their ongoing occupation of Iraq.</P>


