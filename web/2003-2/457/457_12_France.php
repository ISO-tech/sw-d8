<P><font face="Times New Roman, Times, serif" size="4">Millions in the streets</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Strikes over pension cuts rock France</b></font></P><p><font face="Arial, Helvetica, sans-serif" size="2">June 13, 2003 | Page 12</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>JOHN MULLEN reports from France on the latest strikes shaking the country.</b></P>

<P><font face="Times New Roman, Times, serif" size="3">A WAVE of mass strikes continues in France against government plans to slash pensions. On June 3, some 1.5 million people demonstrated across the country as walkouts shut down libraries, train service, tax offices, hospitals, schools and bus depots. Almost all flights were cancelled when air traffic controllers joined the strike. </P>
<P>In the following days, tens of thousands of workers voted to stay out on strike until the pension proposal is defeated. Another day of action was being planned for June 10 as <I>Socialist Worker </I>went to press.</P>
<P>The right-wing government of President Jacques Chirac and Prime Minister Jean-Pierre Raffarin wants to extend the number of years people have to work before they can retire with full benefits--to 40 years, from the current 37.5 for public sector workers, with a further increase to come for all workers. The shift to 40 years was carried out for private-sector workers without a mass response in 1993. </P>
<P>But in 1995, public-sector workers defeated a similar proposal after millions of strikers paralyzed the economy for weeks. Today, polls say that 65 percent of the population support the strikers, and only 27 percent support the government. </P>
<P>This latest wave of struggle has radicalized groups of workers. All last week, teachers blocked bus depots and trains with sit-in demonstrations. In Toulouse, last Thursday was declared "dead town day," as workers sat in on the major roads in and out of the city. On Friday, truck drivers blocked oil refineries.</P>
<P>In a historic shift, it is teachers and other educational staff who are at the center of the movement. Some schools have been on all-out strike for more than two months, and many others have been out for several weeks. </P>
<P>The organization of the strikes has been tremendously democratic. Every day in every workplace, mass meetings vote on whether to continue the strike for another day or not. </P>
<P>French law gives reasonable legal protection for strikers, so any sizeable minority can walk out without necessarily convincing the whole workplace.  Strikers are learning tremendously quickly, and "inter-professional" meetings--where bus drivers, teachers and other workers come together to discuss the movement--are now happening in almost every town. </P>
<P>The government is clearly scared of the strike movement. For example, it has already postponed other attacks on education that were important to school staff. Nevertheless, it is determined not to back down. "This is about the survival of the republic," Prime Minister Raffarin said in a speech to France's parliament. Police attacks on strikers are on the rise, while slanders about the movement being "manipulated by Trotskyists" or "opening the gates" to French fascist Jean-Marie Le Pen have been heard.</P>
<P>It is too early to say whether the movement will build to the kind of sustained general strike needed to win. Private-sector workers are beginning to join in the strike, but this essential development remains very fragile.</P>
<P>Union leaders are putting the brakes on. Leaders of one of the major union confederations in France signed an agreement with the government on pensions. Several transportation unions have opposed the strike, pretending to believe the government's promises that separate pension systems for public transport workers won't be affected. </P>
<P>In general, union leaders are pushing for limited action--days of action once a week at most. They have ignored the sentiment for unlimited strike action. This is what makes the ongoing strikes, especially in education, so impressive--that they have taken place most of all because of the initiative of workers at the rank-and-file level.</P>


