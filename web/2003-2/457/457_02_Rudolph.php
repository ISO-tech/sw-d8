<P><font face="Times New Roman, Times, serif" size="4">Homegrown terrorist caught after evading FBI hunt</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why was he on the loose for so long?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 13, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">JOHN ASHCROFT'S witch-hunters had no problem rounding up hundreds of Arabs and Muslims to lock away as part of the September 11 investigation. But when it came to a white supremacist suspected of bombing abortion clinics, the Feds came up empty-handed for more than five years.</P>
<P>On May 31, local police finally arrested Eric Robert Rudolph in Murphy, N.C. Rudolph, reportedly a member of the Nazi Christian Identity movement, is accused of setting off bombs at two abortion clinics, a gay nightclub and the 1996 Summer Olympics in Atlanta.</P>
<P>In January 1998, he allegedly bombed the New Woman All Women Health Care Center in Birmingham, Ala., killing a guard and maiming nurse Emily Lyons. Lyons had 18 operations over three years to patch her body back together and remove all the shrapnel from her wounds.</P>
<P>"[E]very day he's with me," Lyons told CNN last week. "My face is damaged and when I see it, I know who did it to me. The rest of my body, I look at it, and it's not the body I used to have, and the only reason I don't have what I did is because someone disagreed with what I believed in." In all, Rudolph is suspected of killing two and injuring more than 100 people with his bombs.</P>
<P>FBI officials claimed that the search for Rudolph was one of the most intense manhunts in U.S. history--and Attorney General Ashcroft was quick to take credit for his capture. "Working with law enforcement nationwide, the FBI always gets its man," Ashcroft declared. He's got to be kidding. </P>
<P>Rudolph was caught by accident--an accident that the FBI played no part in whatsoever. A 21-year-old rookie cop with the Murphy Police Department, Jeffrey Postell, thought that a homeless man digging through a dumpster behind a supermarket was a potential burglar and cornered him.</P>
<P>But the homeless man that he was harassing turned out to be Rudolph--one of the FBI's "Ten Most Wanted." The FBI must be kicking themselves that a na&iuml;ve rookie like Postell caught one of their "Ten Most Wanted" literally through the accident of harassing a homeless man.</P>
<P>It shouldn't be a surprise that the Feds had nothing to do with catching Rudolph. From the beginning, the FBI bungled the investigations--for example, initially focusing on part-time security guard Richard Jewell as the main suspect in the Olympic bombing. Jewell was later cleared of any wrongdoing, but not until his life had been ripped apart by the media.</P>
<P>So why did it take so long to catch Rudolph? In Ashcroft's America, the government just doesn't seem to care about homegrown right-wing terrorism.</P>


