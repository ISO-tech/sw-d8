<P><font face="Times New Roman, Times, serif" size="4">The Bush gang's bigots take aim at gay rights</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>No pride in Ashcroft's Justice</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 13, 2003 | Page 4</font></p>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</B>,</P>
</I><P>Attorney General John Ashcroft and the bigots at the Justice Department are at it again. Last week, Justice Department officials told DOJ Pride--a group that represents several hundred gay and lesbian employees at the Justice Department--that it can't hold its seventh annual Gay Pride event because the White House doesn't "formally recognize" Gay Pride Month.</P>
<P>Coming on top of Sen. Rick Santorum's (R-Penn.) recent comparison of homosexuality to bigamy and incest, it's just further proof of how little the Republicans care about our rights.</P>
<P>Ashcroft, for example, has recommended several anti-gay judges for the federal courts. One of them, Ninth Circuit Court of Appeals judge Jay Bybee once argued for a Defense Department program that screened for "all known or suspected" homosexuals seeking top-secret clearance. Bybee also said that gays and lesbians are guilty of "moral turpitude, poor judgment, or lack of regard for the laws of society." </P>
<P>Several congressional Democrats immediately responded to this latest outrage--as well they should. Senator Frank Lautenberg (D-N.J.) said he would propose legislation addressing the issue if the Justice Department didn't allow the event to take place. </P>
<P>Unfortunately, though, we can't count on the Democrats to fight for gay rights. Don't forget Bill Clinton. When we was asking for votes, he talked about supporting gay rights. But when push came to shove, he was all too willing--along with many other Democrats--to support the disastrous "don't ask, don't tell" for gays and the military, and to sign the Defense of Marriage Act, which bars gay couples from marrying.</P>
<P>With Democratic leaders arguing that the party should try to attract more mainstream "swing" voters, don't expect them to be put up a real fight for gay rights anytime soon. Instead of the Bush administration bigots making us nostalgic for a Democratic in office, it should make us angry--and inspire us to take the fight for gay rights to where it will make a difference: in the streets.</P>

<B><P>Nicole Colson, </B>Chicago </P>


