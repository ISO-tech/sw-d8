<P><font face="Times New Roman, Times, serif" size="5"><b>Insurgent images to further the struggle</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Elizabeth Lalasz</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 13, 2003 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">BOOK: Mike Alewitz and Paul Buhle, <B><I>Insurgent Images: The Agitprop Murals of Mike Alewitz, </B></I>Monthly Review Press, 2002, 160 pages, full-color plates, $27.95.</font></P>

<I><P><font face="Times New Roman, Times, serif" size="3">INSURGENT IMAGES </I>chronicles the work of Mike Alewitz, who has been a muralist and labor activist for three decades. Alewitz grew up in a working-class family in the industrial town of Wilmington, Del. As a student at Kent State, he threw himself into activism in 1970 after the National Guard shot and killed anti-Vietnam war protesters.</P>
<P>At the University of Texas-Austin, he began using his artistic talents for political organizing, staying up all night before protests lettering slogans and making posters. Alewitz connected art to the struggle, as part of a long tradition of political artists, like Mexican muralist Diego Rivera, who believed art should be an expression of the movement, not isolated commentary in a gallery.</P>
<P>During the 1985-86 Hormel meatpackers strike in Austin, Minn., Alewitz and P-9 strikers together created a mural integrating the image of a large serpent--representing the company--being cut apart by two meatpackers--male and female. They combined a serpent, an image from the Russian Revolution, with present-day images of the strike.</P>
<P>Strikers also included images of South African workers protesting apartheid, a struggle half a world away that the workers saw as like their own. The book describes how strikers protected the mural from company vandals and how the international union, not Hormel, destroyed it after they took over the local.</P>
<P>Again and again, Alewitz repeated the P-9 experience, involving ordinary workers in painting their own murals, as visible testimonies of their fights. As Alewitz acknowledges, "...to all the workers whose brave actions laid the basis for this book...I consider you to be the authors of these works as much as I am. Without you I would have nothing to paint."</P>



