<P><font face="Times New Roman, Times, serif" size="4">Ashcroft and his witch-hunters</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Tearing up our civil liberties</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 13, 2003 | Page 12</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"THE WAR on terror quickly turned into a war on immigrants." That was the conclusion of ACLU Executive Director Anthony Romero last week in the wake of a new report by the U.S. Justice Department's own inspector general.</P>
<P>The report details a pattern of abuses in the investigation into the September 11, 2001 attacks--and concludes that most of the department's arrests of 762 people on immigration violations were plagued with "significant problems." Meanwhile, the number of people held on material witness warrants and other minor charges remains unknown.</P>
<P>Among the findings of in-house investigators:</P>
<P>--Detainees were left in jail cells an average of 80 days before being released or deported.</P>
<P>--At the Metropolitan Detention Center in New York City, some officers routinely threw inmates against walls before videotaping their statements. Some would taunt inmates by saying, "You're going to die here." </P>
<P>--Family members searching for loved ones were often lied to outright--told that their family members weren't at facilities, when they in fact were.</P>
<P>--Since undocumented immigrants are not technically entitled to a lawyer, some detainees at the MDC were purposefully given the wrong phone numbers for lawyers by authorities. In other cases, they were given phone numbers for lawyers who authorities knew were unwilling to represent them. A wrong number or busy signal could be counted as a detainee's one "legal call" per week.</P>
<P>The disgusting treatment outlined in the report only confirms what immigrants' rights advocates and civil liberties groups have been saying for months--that John Ashcroft's Justice Department threw the Constitution out the window after September 11.</P>
<P>Deputy Attorney General Larry Thompson is quoted as saying that the department was pursuing "a different kind of law enforcement approach." That's right--an approach where any immigrants rounded up were guilty until <I>proven </I>innocent.</P>
<P>In what can only be described as racial profiling, some detainees were picked up after anonymous tips consisting of little more than that they were Muslims with irregular schedules. One man, for example, was arrested after an acquaintance wrote to officials claiming that he had made "anti-American statements."</P>
<P>Both the FBI and CIA had cleared this detainee of any connections to terrorism by mid-November 2001. Yet he wasn't released until February 2002--because of an "administrative oversight."</P>
<P>"I think [the report] illustrates what we have been pointing out all along," Fred Tsao, policy director of the Illinois Coalition for Immigrant and Refugee Rights, told <I>Socialist Worker.</I> "This wave of detentions that took place right after September 11 was a vast and gross overreaction--particularly in light of the fact that it's netted nobody who was actually involved in terrorism, and was so particularly focused on people from Middle Eastern and predominantly Muslim countries. We knew at the time that this was a wrong activity, and I think our position's been vindicated."</P>
<P>Don't try telling that to Ashcroft. Taking the offensive during testimony before Congress last week, the attorney general declared that the detentions are part of a "solid foundation" to prevent another terrorist attack.</P>
<P>Likewise, Justice Department spokesperson Barbara Comstock smugly declared: "We make no apologies for finding every legal way possible to protect the American public from further terrorist attacks." But Ashcroft and his gang hunters at the Justice Department are the ones we really need protection from.</P>
<P>Appearing in front of the House Judiciary Committee last week, the chief witch-hunter asked for <I>expanded </I>powers--to hold terrorist suspects indefinitely before trials and to seek the death penalty or life imprisonment for any terrorist act, which includes giving "material support" to a suspected terrorist group. We have to stand up to these new attacks--and tell Ashcroft's In-Justice Department that we won't let them take away our rights.</P>



