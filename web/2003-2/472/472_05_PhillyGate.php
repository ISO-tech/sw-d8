<P><font face="Times New Roman, Times, serif" size="5"><b>Echo of Watergate in Philadelphia?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 17, 2003 | Page 5

<P><font face="Times New Roman, Times, serif" size="3">WHAT'S BUGGING Philadelphia's Democratic Mayor John Street? The FBI, it turns out.</P>
<P>Last week, several FBI listening devices were found hidden in the mayor's office. The FBI admitted that Street is the "subject"--but not the "target"--of an investigation, but wouldn't say more than that.</P>
<P>Street's supporters accuse Attorney General John Ashcroft and the Justice Department of staging an "October surprise" that could result in Street's defeat in the November election. The aim, they suggest, is to put the mayor's office under Republican control--and help George W. Bush in the 2004 election. As several commentators pointed out, the "shades of Watergate" were unmistakable.</P>
<P>Dirty tricks wouldn't be surprising. Street's competitor, Republican businessman Sam Katz, is as vile as they come. In the last match-up between the two, in which Street eked out a narrow victory, Katz used racist ads showing white cops chasing down Black teenagers to promote a "tough on crime" stance. In the current race, Republican supporters of Katz have drawn fire for distributing flyers to white residents that call to "take back Philly."</P>
<P>But Street is no angel, either. Part of the city's Democratic Party machine, he's spent years pouring cash into the pockets of well-connected cronies. In June, he was forced to rescind a $13.6 million maintenance contract at Philadelphia International Airport which had been granted to a subsidiary of Enron--when it was discovered that Enron had then subcontracted $1.2 million worth of business to Street's brother.</P>
<P>Investigators are also looking into reports that a city official threw out more than 125,000 parking tickets--many issued to Democratic officials and political leaders--with potential fines of $6.1 million. In the end, the city of "brotherly love" would be better off without both Katz and Street.</P>



