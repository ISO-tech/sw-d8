<P><font face="Times New Roman, Times, serif" size="4">Nightmare in Iraq</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bush doesn't care how many die</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 17, 2003 | Page 1

<P><font face="Times New Roman, Times, serif" size="3">GEORGE W. BUSH'S justifications for invading Iraq have been exposed as a massive fraud. And every day brings more examples of the mayhem and violence of Washington's colonial occupation.</P>
<P>The latest blow to the Bush administration's claim that all resistance in Iraq is the work of "terrorists" and "Saddam loyalists" came last week in Sadr City, the poverty-stricken, Shiite-dominated neighborhood in northern Baghdad. A car bombing that killed eight Iraqi police spiraled into a street battle between Iraqis and U.S. soldiers that left two Iraqis dead. Infuriated by the deaths, as many as 10,000 Shiites poured into the streets of Sadr City, chanting "No, no to America!" in an outraged protest against the occupation.</P>
<P>Until now, most attacks against U.S. troops have been focused in the so-called "Sunni triangle"--where, the U.S. military contends, pockets of former officials of Saddam Hussein's regime plot against the occupiers. But in a further sign of the crisis facing Washington, Shiites--a group oppressed under the old regime--are increasingly turning against the U.S. presence.</P>
<P>"I asked my interpreter if the Iraqi people are mad at us," Private T.J. Knight told the <I>San Francisco Chronicle.</I> "He said that 90 percent of Iraqis hate us, and the other 10 percent have left Iraq." </P>
<P>It's no wonder why so many Iraqis hate the U.S. The arrogance and brutality of Washington's occupation is on display every day, in a hundred humiliating ways.</P>
<P>There's the chronic shortages of basic necessities like electricity and water, months after the U.S. promised it would begin "reconstruction." There's the wholesale sell-off of Iraq's industries to U.S. businesses and Bush administration cronies. And there's the cruel examples of iron-fisted repression most reminiscent of Israel's war on the Palestinians.</P>
<P>According to <I>Independent</I> journalist Patrick Cockburn, U.S. troops recently demolished dozens of fruit groves near Dhuluaya--as part of a new policy of "collective punishment" for farmers who are suspected of withholding information about guerrilla attacks. "They told us that the resistance fighters hide in our farms, but this is not true," said Nusayef Jassim, one of 32 farmers who saw their fruit trees--and livelihoods--destroyed.</P>
<P>Meanwhile, U.S. soldiers feel increasingly bitter at what the conditions they are forced to endure--and the orders that they have to carry out--for Washington's occupation. White House spin doctors claim that the Iraqi resistance is a "bump in the road," as U.S. colonial overseer Paul Bremer put it. </P>
<P>But the truth is that as long as the U.S. remains in Iraq, the resistance will continue. On October 25, we have to stand up and say no to Bush's occupation for oil and empire--and speak out in solidarity with the U.S. soldiers who want to return home now.</P>



