<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A crucial step for our movement</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 17, 2003 | Page 3

<P><font face="Times New Roman, Times, serif" size="3">ACTIVISTS AROUND the country are heading for Washington, D.C., and San Francisco on October 25 for national demonstrations demanding an end to the occupation of Iraq. These protests, sponsored jointly by the two biggest national antiwar coalitions, International ANSWER and United for Peace and Justice, come at a critical time for the antiwar movement.</P>
<P>They are the first national demonstrations since the invasion of Iraq began in March. Before the war, millions of people took to the streets around the world to protest the coming slaughter. Now, with the U.S. presiding over a brutal occupation, it's critical that activists in the U.S. stand in solidarity with the Iraqi people resisting their occupiers.</P>
<P>We must also standing in solidarity with the working-class American soldiers who every day grow more aware that they are little more than cannon fodder for Bush's war for oil and empire. For these reasons, the slogan of the October 25 protests is "End the Occupation, Bring the Troops Home!"</P>
<P>The October 25 demonstrations won't be as large as the millions-strong protests that preceded the war. The urgency of the actions to stop the war before it began brought together a wide range of people--longtime activists and people who had never protested before, pacifists, liberals, socialists, unionists and students--in unprecedented numbers.</P>
<P>Today, the situation is more complicated. While the senseless brutality of the occupation is clear, the focus for the movement's demands is less obvious.</P>
<P>Some peace activists believe that the U.S. should stay in Iraq until it has "made things right." For others, the solution is for the United Nations (UN) to take over the occupation. ''We need to get the UN in and the U.S. out,'' said Dennis Kucinich, the Democratic presidential hopeful who is widely viewed as the antiwar movement's candidate, at a recent protest in Los Angeles. ''There can be a way to extricate this nation from the quicksand of Iraq.''</P>
<P>But a UN occupation won't bring democracy or liberation to the people of Iraq any more than the U.S. will. The fact that the Bush administration was forced to appeal to the UN for help last month is a welcome chink in Bush's armor. But it's a concession on how--not whether--the U.S. will call the shots in Iraq.</P>
<P>These are important debates at this stage of the occupation--and it's inevitable that they will translate into differences between the left wing and right wing of the antiwar movement. But even if they aren't as large as the prewar protests, the October 25 demonstrations are just as important--for they represent the beginning of a new stage of the struggle.</P>
<P>Among the public at large, more people than ever question the Bush war machine and its cynical exploitation of the September 11 attacks to launch a war on the world. October 25 is our opportunity to give a concrete expression to these doubts--and take important new steps in organizing an ongoing opposition to Washington's occupation of Iraq and its military adventures around the world.</P>



