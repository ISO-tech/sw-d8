<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Unraveling occupation of Iraq sparks White House infighting</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The empire stumbles</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 17, 2003 | Page 3

<P><font face="Times New Roman, Times, serif" size="3">WHAT THEY really need is a White House Stabilization Group. The latest round of infighting in the Bush administration began when National Security Adviser Condoleezza Rice announced that she would head a new Iraq Stabilization Group--a power grab at the expense of Defense Secretary Donald Rumsfeld over his responsibility for the disastrous occupation of Iraq. Rumsfeld blew his top, complaining to reporters that he learned about the change in the newspapers.</P>
<P>The flap undercut a coordinated PR campaign by the White House to sell the occupation. When Vice President Dick Cheney lashed out at critics in a speech last week, he got big applause--but only because the audience was at the Heritage Foundation, the latest of a series of right-wing venues visited by administration heavies. "Where are they going to speak next?" asked Matt Rothschild, editor of <I>The Progressive </I>magazine. "The Halliburton annual board meeting?"</P>
<P>Back in the real world, though, criticism and doubts about the occupation are growing with each death of a U.S. soldier. Meanwhile, the Iraqi resistance--fueled by suffering and economic misery at the hands of the U.S. occupiers--is gathering momentum.</P>
<P>With the U.S. military over-stretched by commitments around the globe, the administration is once again looking for cover at the United Nations (UN). But even if the UN does agree to take a greater role in Iraq, "overall control on the ground will remain in the hands of the occupying power," the BBC reported.</P>
<P>Behind these latest diplomatic maneuvers is the ugly reality of great-power politics, in which a handful of powerful nations dominate the rest of the world--with regional powers on the payroll as their deputies. That's why the Turkish parliament agreed to send 10,000 troops to join the U.S.-British occupation of Iraq--right after it got an $8.5 billion loan from Washington.</P>
<P>Beyond the cash, Turkey is eager to assert its authority to prevent Kurdish mini-states created by the U.S. in northern Iraq from reviving the independence movement in Turkish Kurdistan. Given Turkey's historical role as a colonial ruler of Iraq, even the puppets on the handpicked Iraqi governing council are complaining about the deal.</P>
<P>In any case, U.S. officials have calculated that it is worth conceding some power to the UN, if this brings a decline in American casualties. That's a retreat from the Bush Doctrine--the U.S. policy of go-it-alone, pre-emptive military action.</P>
<P>The Bush gang sees this as a necessary short-term concession in the pursuit of their long-term goal of redrawing the map of the Middle East through more military interventions and "regime changes." The chaos in Iraq since Washington's "victory" has exposed this idea as a right-wing fantasy. But no one should conclude that the U.S. is about to pick up and leave.</P>
<P>Such a retreat would be a catastrophic defeat for U.S. imperialism--which is why none of the leading Democrats seeking their party's presidential nomination calls for an immediate withdrawal of U.S. troops. For example, Sen. John Kerry (D-Mass.) said at the October 9 presidential debate that the U.S. should "go to the United Nations now and get rid of the sense of American occupation in Iraq."</P>
<P>Gen. Wesley Clark agreed that the U.S. should "turn the economic and political piece over to the United Nations," but insisted that "we need to keep control of the military piece and support our armed forces." Howard Dean, who postures as an antiwar candidate, chimed in, saying, "Now that we're there, we can't pull out responsibly."</P>
<P>In other words, the Democratic candidates don't disagree that the U.S. should use its military might to dominate the Middle East. They just think that Bush botched the job. Bush Doctrine or not, U.S. imperialism has Iraq in its grips and won't let go--until it's forced to do so by mounting resistance in Iraq and an anti-occupation movement at home.</P>


