<P><font face="Times New Roman, Times, serif" size="4">Minority students targeted at University of Texas</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Stand up against racism</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 17, 2003 | Page 4

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</I>,<BR>
</B>A series of incidents at the University of Texas has shown that racism is alive and well on this campus.</P>
<P>Following a sensationalistic article in the student newspaper about an international student who forged his application information, the Young Conservatives of Texas distributed "Wanted" posters, accusing the student of terrorism. The same group has also sponsored an "affirmative action bake sale," which portrays minority students as undeserving of admission to college.</P>
<P>In the same week that the "Wanted" flyers were distributed, the National Alliance (NA)--a neo-Nazi hate group--put up posters on campus targeting Black men. With about 6,000 members nationally, the National Alliance believes that it is necessary to eliminate people of color from the earth and to "reclaim" it for the white race. </P>
<P>These are people involved in conducting--and urging others to conduct--racist violence. Even if their flyer emboldens or recruits only one or two people from their campus blitz, it should be taken very seriously. The NA's hatemongering should be challenged, not on the basis of university speech restrictions (which supposedly bar "off-campus" organizations), but because they use the racist idea of lynching in a coded appeal to hard-core racist vigilantes, who may engage in violence against Blacks and other people of color.</P>
<P>The Young Conservatives are not Nazis, but their flyer targeting a Pakistani student is likewise an example of racist vigilantism that represents a real threat to international students. The Pakistani student organization has expressed fear about holding public activities in the wake of the threat, for fear that any person may accost them or report them to Homeland Security--which has shown no regard for civil liberties or restraint in rounding up members of the international community for imprisonment, interrogation and deportation.</P>
<P>It is urgent that students, faculty, and staff stand up publicly against this upsurge in racism on campus. These actions must be met with a strong demonstration of racial inclusion, support for minority and international students and a refusal of the racism that these messages represent.</P>
<B><P>Dana Cloud and Mike Corwin, </B>Austin, Texas</P>


