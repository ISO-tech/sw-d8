<P><font face="Times New Roman, Times, serif" size="5"><b>Palestine mural under threat</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 17, 2003 | Page 5</P>

<P><font face="Times New Roman, Times, serif" size="3">ISRAELI AUTHORITIES have ordered the demolition of the Shawamreh family's house in the West Bank village of Anata--for the fifth time. The Shawamreh family has spent a total of $20,000 in four attempts to get a building permit--and has been denied again and again.</P>
<P>Each time, the official excuse is different--but the real reason is that Israeli officials want to confine Palestinians to as small an area of the Occupied Territories as possible. Since 1967, Israel has destroyed more than 10,000 Palestinian homes in the Occupied Territories.</P>
<P>But this time, there's a special reason that Israeli authorities want to destroy the Shawamreh house. This past summer, hundreds of volunteers from all over the world rebuilt the house--and U.S. labor muralist Mike Alewitz painted a mural over an entire exterior wall, commemorating Rachel Corrie and Nuha Sweidan, two women killed by Israeli troops carrying out home demolitions.</P>
<P>Though authorities issued a demolition order two weeks ago, a lawsuit has--at least for now--prevented it from being carried out. "They want to knock the house down because it's a symbol of resistance of the Palestinian people," Alewitz told <I>Socialist Worker.</P>
</I>
