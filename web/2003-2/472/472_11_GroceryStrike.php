<P><font face="Times New Roman, Times, serif" size="4">70,000 workers strike in Southern California</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Staying strong on the line"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Evan Kornfeld and Gillian Russom</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 17, 2003 | Page 11</P>

<P><font face="Times New Roman, Times, serif" size="3">LOS ANGELES-Some 70,000 grocery store workers in Southern California hit the picket lines October 12 at the Von's, Ralph's and Albertson's supermarket chains. The supermarkets had refused to alter its demands for reductions in health benefits and for pay cuts for new hires.</P>
<P>Rick Icaza, president of United Food and Commercial Workers (UFCW) Local 770, told the <I>Los Angeles Times</I> that the union had made proposals to cutting health costs, but the chains had refused to consider them. Union leaders instructed workers to strike only at Von's--which is owned by the grocery giant Safeway--in order, they said, "not to inconvenience customers."</P>
<P>But Albertson's and Ralph's--owned by another industry powerhouse, Kroger--both locked out the union. The employers want each worker to pay at least $1,300 a year for insurance premiums, which could add up to $3,500 a year for a family.</P>
<P>They're demanding big increases in health care deductibles and co-pays for doctor visits and for prescription drugs. Oscar Herrera, a picket captain at Von's in the Echo Park area of LA, told <I>Socialist Worker</I>, "Benefits is what everyone is fighting for. That's why people stay with these companies. Our slogan is to stay strong on the line and not let up."</P>
<P>Besides cuts in benefits, the employers are demanding a two-tier pay system in which new hires would top out at $14.90 per hour, compared to the current $17.90. They also want managers to have discretion over how many hours to give to employees, so more hours would go to lower-paid new hires.</P>
<P>Employers are also want "unlimited use of vendors, brokers, salesmen, etc." to do union work. Management even demands the right to open non-union stores in areas where the unionized market share is less than 25 percent!</P>
<P>The chains claim these givebacks are necessary because of the threat of non-union superstores such as Wal-Mart. Yet the corporations that own the three supermarket chains had combined net profits of $1.2 billion for the first half of this year-and their operating profits have risen 10 times faster than their hourly cost for worker health care in Southern California over the past 10 years. Their combined profits have increased 98 percent just since 1998.</P>
<P>On October 10, the three chains took out full-page newspaper ads claiming that they're only demanding that workers pay $5 to $15 per week for health benefits--failing to mention the 50 percent cut in benefits and the two-tier wages. The chains further insulted their employees by placing huge signs in their store windows advertising for "replacement workers"--scabs.</P>
<P>Many customers relate to the workers' fight for decent health care and honored the picket lines. At the Von's in Hollywood, one shopper stopped his SUV to talk with the strikers. "You tell your managers that I spend $500 dollars a month here-but I won't be shopping until they give you guys what you deserve."</P>
<P>On the picket line, workers agreed that they had to strike. "Most important is our health care," said Fuensanta Espinoza. "I have kids. If they get sick, why should we have to pay so much?" Patty Gutierrez, a young Von's employee, doesn't have a family to support but said, "They want to take away my dental and my vision insurance. My contacts would cost me over $300. They're taking away a lot of things we depend on."</P>
<P>The UFCW is also striking some supermarket chains in the St. Louis area, and could soon face strikes in Wisconsin and in West Virginia as well. A victory for workers in Southern California would help turn back the employers' offensive-not just in the grocery industry, but in others as well. As Lily, who's been a cashier at the Echo Park Von's for 15 years, told <I>Socialist Worker</I>, "To win, the most important thing is the unity of everyone. We can't let them intimidate us."</P>



