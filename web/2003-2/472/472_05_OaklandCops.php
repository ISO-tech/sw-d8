<P><font face="Times New Roman, Times, serif" size="4">Oakland thugs cleared by mostly white jury</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Crooked cops walk free</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By John Green and Steven Damewood</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 17, 2003 | Page 5</P>

<P><font face="Times New Roman, Times, serif" size="3">THE LEADERS of a gang of crooked and brutal Oakland cops known as the "Riders" went free at the end of September. After a trial that lasted more than a year, three ex-Oakland cops were found not guilty on eight charges of abuse. The jury deadlocked on another 27 criminal charges.</P>
<P>The three cops--Matthew Hornung, Clarence Mabanag and Jude Siapno--were accused of planting evidence, kidnapping and assault, among other allegations. Frank Vasquez, the ringleader of the Riders, is believed to have fled the country.</P>
<P>The case against the three thugs should have been open and shut. The Oakland Police Department has already fired them--and the city has paid their victims close to $11 million to settle civil lawsuits. But Alameda County District Attorney Tom Orloff obviously wasn't too concerned about winning this case.</P>
<P>Oakland's population is more than one-third African American, and most of the Riders' victims were Black. But the jury for the Riders case was predominately white. The only two Black jurors were alternates--with no rights to participate in the months of jury deliberations.</P>
<P>"This blows me away," one alternate told the <I>San Francisco Chronicle.</I> "I can't believe this. They are so guilty. The evidence was overwhelming. Most Black people know that police can lie to make an arrest, but I think the people on this jury don't believe it's possible for police to lie. They just don't get it."</P>
<P>In the outcry following the verdict, several jurors came forward to explain how deliberations went. According to them, the jury foreman--a white male law student, who declared from the beginning that he believed the cops were innocent--formed a bloc with two other conservative jurors during the months of deliberations. They were suspicious of charges of police brutality toward minorities, other jurors said, and at least one made a racist comment. </P>
<P>"In most trials in Alameda County, there are African Americans on the jury--and then you have a case in which all the victims are African American and, there are no African-American jurors," said attorney John Burris, who negotiated the city's payout over the Riders last fall. "That's a fact that's hard to swallow."</P>
<P>Now, the three cops--smug after their victory in the courtroom--have said that they may reapply to become Oakland cops. Since the verdict was announced, activists, residents and victims of police brutality have been organizing rallies outside the courthouse to expose the truth--that the real criminals are the cops who terrorize Black neighborhoods. We have to keep up this pressure--and stop Oakland's cops from going back to meting out vigilante justice.</P>



