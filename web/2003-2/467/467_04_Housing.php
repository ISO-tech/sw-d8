<P><font face="Times New Roman, Times, serif" size="5"><b>Leaving poor families behind</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 12, 2003 | Page 4</font></p></P>

<P><font face="Times New Roman, Times, serif" size="3">MORE THAN 100,000 low-income families could be thrown out on the street next year under a spending bill passed September 4 by a Senate committee and recently approved by the House. Under the federal housing voucher program for poor families--known as Section 8--vouchers pay the difference between the market rent of an apartment and 30 percent of a household's income.</P>
<P>The program subsidizes more than 2 million families a year. But House members based spending this year on an outdated figure for housing costs. Once the higher cost is factored, a gap remains in funding for some 114,000 families.</P>
<P>Housing advocates argue that the rising cost of housing, added to high rate of unemployment, had pushed up the costs of subsidies--and Congress needs to take this into account. "The appropriations committees are operating under extremely narrow confines of what they can do," said National Low Income Housing Coalition President Sheila Crowley.</P>
<P>"Now they're not even spending enough money to sustain the number of people who currently pay for housing through the voucher program. It's an unprecedented break in the commitment made when the program was founded in 1974."</P>



