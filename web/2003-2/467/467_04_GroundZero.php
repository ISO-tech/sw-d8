<P><font face="Times New Roman, Times, serif" size="4">How the Enviromental Protection Agency lied about...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Toxic hazards at Ground Zero</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Joe Cleffie</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 12, 2003 | Page 4</font></p></P>

<P><font face="Times New Roman, Times, serif" size="3">THE BUSH administration has held up the post-September 11 rescue operations at the World Trade Center as a symbol of national unity and pride. But revelations about how the Environmental Protection Agency (EPA) was manipulated into lying about air safety around Ground Zero shows the contempt that Bush and his pals have for ordinary people. </P>
<P>Now, in a protest that will throw more light on the scandal, Sen. Hillary Rodham Clinton (D-N.Y.) is blocking the confirmation process for Bush's candidate to head the EPA, Mike Leavitt, until the administration comes clean. Leavitt was nominated to replace Christine Todd Whitman, who was in charge of the EPA after September 11, when staffers were directed to "remove words of caution and replace them with reassuring statements," in the words of an internal agency investigator.</P>
<P>On September 16, the agency issued a statement claiming, "Our tests show that it is safe for New Yorkers to go back to work in New York's financial district." But the EPA didn't even test the grounds for contamination until more than a week later.</P>
<P>The frightening list of possible toxic hazards at Ground Zero includes asbestos, PCBs, chromium, benzene, mercury and lead. But the EPA said nothing--because of "national security and a desire to reopen Wall Street," according to the agency's inspector general.</P>
<P>This blatant disregard for the health of rescue workers and area residents is already starting to show itself. In a test of more than 7,000 workers in the area, 48 percent had developed respiratory problems such as asthma, chronic congestion and coughing. More than 40 percent of babies born to women who are residents of downtown Manhattan during the last two years have had an average birth weight of a half-pound lower than normal. </P>
<P>Dr. Steven Levin of Mount Sinai Hospital, who has worked on many of the studies of workers at Ground Zero, summed up the anger over the recent revelations. "People went back to their apartments because Christie Todd Whitman said it was safe," Levin told New York <I>Newsday. </I>"I have patients who knew it was wrong--they could feel it. But their employer said, 'The EPA says it's safe.'" Levin says that the worst may well be yet to come. </P>
<P>For example, it will be another 20 to 30 years before the full effects of poisoning from asbestos are discovered. The truth that is now emerging about the hazards at Ground Zero shows that the Bush gang never cared about ordinary people. They only cared about justifying their war agenda. </P>



