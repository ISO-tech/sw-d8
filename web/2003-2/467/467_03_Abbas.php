<P><font face="Times New Roman, Times, serif" size="4">Abbas resignation throws U.S. "peace" plan into disarray</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Israel tears up road map</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 12, 2003 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">THE RESIGNATION of Palestinian Prime Minister Mahmoud Abbas last week dealt a devastating blow to the U.S.-backed "peace plan" trotted out four short months ago. And within hours of Abbas' announcement, Israel added to the turmoil by unleashing another attack on the militant Islamist organization Hamas, firing a 500-pound bomb at a building where Hamas cofounder Sheik Ahmed Yassin was having lunch. </P>
<P>The bomb destroyed the building, but Yassin escaped with only a slight injury. "We heard a loud noise, and then everything went black, and then red before my eyes," said Marwan Abu Ras, a university lecturer and Hamas official whose home was destroyed by the blast.</P>
<P>Though the attack injured 12 adults and five children, Israeli Prime Minister Ariel Sharon made no apologies. And no reporters remarked on the obvious fact that Israel's assault--like its other targeted killings--showed the flagrant disregard for civilian life that it constantly accuses Palestinians of. "We heard a loud noise, and then everything went black, and then red before my eyes," said Marwan Abu Ras, a university lecturer and Hamas official whose home was destroyed by the blast.</P>
<P>Sharon followed up the failed assassination attempt with a threat against all Hamas members--declaring, "They are marked for death." In response, Hamas attacked Israel for opening "the gates of hell" and vowed to retaliate.</P>
<P>The escalating confrontation is no surprise. In explaining why he resigned, Abbas blamed Israel for stepping up its campaign of assassinations and refusal to withdraw more of its troops. And he pointed a finger at the U.S. government for failing to pressure Israel to comply with its "road map" for peace.</P>
<P>Abbas also lashed out at Palestinian leader Yasser Arafat for undermining his authority. Arafat did refuse to fully hand over the reigns of power. But this was only possible because Abbas lacked any real popular support. His only real backers were in the U.S. and Israel, which strong-armed Arafat into appointing a prime minister that they hoped to control.</P>
<P>If Israel had stopped its policy of targeted killings, withdrawn troops from more of the Occupied Territories and released some of the thousands of Palestinian prisoners in its jails, Abbas might have established a base of popular support. "The Israelis could have got rid of Arafat in no time if they had made some concessions to [Abbas]," said Hani al-Masri, a Palestinian information ministry official. "But they were looking for a collaborator who would sign up for a Palestinian state on less than half the West Bank. Israel doesn't want peace, it wants capitulation."</P>
<P>As the Israeli establishment began to openly debate whether to expel Arafat from the Occupied Territories, he quickly moved to appoint a new prime minister, Ahmed Qureia, who is currently the speaker of parliament and known for his accommodating attitude towards Israel. But whatever happens with Qureia, Israel's ruthless assaults on Hamas are bound to escalate the conflict.</P>



