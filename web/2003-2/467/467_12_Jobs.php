<P><font face="Times New Roman, Times, serif" size="4">2.7 million jobs gone under Bush</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A "job-loss" recovery</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 12, 2003 | Page 12</font></p></P>

<P><font face="Times New Roman, Times, serif" size="3">AT LEAST he can say he created one job. Last week, George W. Bush--under increasing pressure for presiding over an economic black hole for U.S. workers--announced that he was appointing a "jobs czar."</P>
<P>The not-so-high-powered title for the still unnamed "czar" is assistant secretary of commerce to oversee the manufacturing sector of the economy--in other words, another invisible bureaucrat to be wheeled out for public relations purposes only. The announcement was an obvious attempt to draw attention away from yet more dismal employment statistics--and Bush's attempt to rip off millions of workers by denying them overtime pay.</P>
<P>The Labor Department reported last week that payrolls across the U.S. economy decreased by 93,000 in August--the seventh consecutive month of declines--even as the unemployment rate dropped a fraction to 6.1 percent.</P>
<P>What this means is a double disaster. The overall number of jobs in the U.S. economy continued to shrink--making today's job market the worst since the Great Depression of the 1930s. But at the same time, so many more of the long-term jobless gave up on finding any work at all and dropped out of the official statistics that the overall unemployment rate actually dropped.</P>
<P>The White House is hyping statistics showing economic growth--even though the U.S. economy has lost 2.7 million jobs since Bush took office. No wonder economists at the Wall Street firm Goldman Sachs are calling this the "job-loss" recovery--a play on the so-called "jobless" recovery that followed the 1990-92 recession.</P>
<P>One result of the lack of jobs is that 1.3 million people fell under the poverty line last year--for a total of 35 million Americans.</P>
<P>The White House solution to this crisis is tried and true--more tax cuts. Bush even had the gall to claim recently that "tax relief is stimulating job creation all across the country."</P>
<P>In fact, since the second tax cut was signed in late May, employers have slashed 225,000 jobs--and counting. "The administration sold the tax cuts as a solution to the jobs problem," Lawrence Mishel, president of the liberal Economic Policy Institute, told the <I>Washington Post. </I>"Just like the schools, the administration should be held accountable for performance."</P>
<P>Workers--both those out of a job, and those still working, but under the gun because of the threat of unemployment--are suffering from Bush's jobs disaster. But this squeeze on workers is a big boost to Corporate America's bottom line.</P>
<P>The Labor Department reported that productivity--the amount that workers produce for each hour of work--soared to 6.8 percent in the second quarter of the year, from April to June. That means that corporate profits are recovering because employers are forcing fewer workers to work harder and longer, and for less money and benefits.</P>
<P>And if Bush and the Republicans stop Democratic legislation to block it, a new federal regulation could allow employers to deny overtime pay to as many as 8 million people--a huge blow to workers who depend on extra hours to make ends meet. </P>
<P>That's the truth about the "job-loss" recovery--and Bush's responsibility for the crisis facing working people.</P>



