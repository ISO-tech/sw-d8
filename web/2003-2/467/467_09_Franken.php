<P><font face="Times New Roman, Times, serif" size="4">What the Republican right really looks like</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Unfair and unbalanced</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 12, 2003 | Page 9</font></p></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">Al Franken, <B><I>Lies (And the Lying Liars Who Tell Them): A Fair and Balanced Look at the Right.</B></I> Dutton, 2003, 377 pages, $24.95.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THERE'S NOTHING right-wing Republicans hate more than being challenged. And there are few people more devoted to this noble cause than writer-comedian Al Franken.</P>
<P>To this end, Franken's new book, <I>Lies (And the Lying Liars Who Tell Them), </I>doesn't disappoint. In <I>Lies, </I>Franken--best-known for his book <I>Rush Limbaugh is a Big Fat Idiot </I>and his years on <I>Saturday Night Live</I>--targets right-wing "journalists" like Ann Coulter, author of <I>Slander,</I> and Bernard Goldberg, who wrote <I>Bias: A CBS Insider Exposes How the Media Distorts the News.</I></P>
<P>With the aid of a crack team of Harvard researchers, Franken slices and dices the right wing's slander and distortions. One of the most hilarious parts takes aim at Bill O'Reilly--or "O'Leilly" as Franken likes to calls him--of Fox News' <I>O'Reilly Factor</I>.</P>
<P>Franken treats us to steamy excerpts from a suspense novel that O'Reilly penned. But best of all is Franken's retelling of his exchanges with O'Reilly at the Los Angeles Book Expo--at first backstage over O'Reilly's unflattering picture on the cover of Franken's book, and later as Franken exposes the right-winger's big fat lies from the podium.</P>
<P>In the end, O'Reilly loses control and starts shouting. The book so outraged the Fox News network that it sued Franken over the subtitle "A Fair and Balanced Look at the Right"--because Fox supposedly had copyrighted the phrase "fair and balanced." Fox lost the suit, and the publicity it generated made sales of Franken's book skyrocket.</P>
<P>But no book titled <I>Lies </I>would be complete without including the world's most dangerous liar, George W. Bush. From the tax cuts that were supposed to help ordinary Americans through the economic crisis, to the weapon of mass destruction that were supposedly the reason that the U.S. went to war on Iraq, Franken deftly pulls these apart the Bush administration lie machine.</P>
<P>But there's something very frustrating about this book--all the opportunities that Franken misses to take on a whole other group of politicians who lie: the Democrats. For every poisoned dart aimed the way of the Republican Right, Franken sends out a sloppy kiss to the Democrats--and the most conservative Democrats at that.</P>
<P>It's too bad that Franken's talent is wasted shilling for people who plainly don't deserve it. Because Franken backs the Democrats all the way, he often ends up justifying its most backward elements for the most backward reasons--for instance, insisting that Clinton's crime bill proved that Democrats are tough on crime, or claiming that Clinton would have won the "war on terrorism."</P>
<P>These are the conclusions that you reach and the arguments that you make when you let the Republicans set the terms of the debate. So while Franken makes some terrific arguments in this book--like a great defense of affirmative action in his chapter on racism--he spends most of his time defending a party that doesn't have the courage, or even the interest, to make these arguments itself. </P>
<P>Franken's wit and willingness to wage a fight to the finish would be well spent going after the rotten polices of both Republicans and Democrats. There's certainly plenty of material.</P>


