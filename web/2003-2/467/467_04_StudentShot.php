<P><font face="Times New Roman, Times, serif" size="5"><b>Student activist shot in West Bank</b></font> </P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Kate O'Neill</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 12, 2003 | Page 4</font></p></P>

<P><font face="Times New Roman, Times, serif" size="3">Another solidarity activist from the U.S. got caught in the crosshairs of Israel's savage crackdown on Palestinians last month. Nasser, a student at Hunter College in New York City, was shot by a rubber bullet August 27 by Israeli forces when they opened fire on a parade commemorating the two-year anniversary of the death of PLO leader Abu Ali Mostafa in Ramallah.</P>
<P>Shooting started when funeral marchers began pelting rocks at Israeli soldiers who entered the city with two armored jeeps and an armored personnel carrier to arrest fifteen young activists at a local organizing center. Nasser had not even reached the demonstration when a rubber bullet grazed his left temple.</P>
<P>According to another Hunter student in the solidarity delegation he was traveling with, his group was in a cab just entering Ramallah when they heard several shots fired in the distance and saw a crowd of Palestinian youth retreating towards them. The students dispersed, and this is when Nasser was struck.</P>
<P>Nasser, a longtime student activist against the budget cuts and the war, was luckily back on his feet after a brief visit to the hospital. But, Zara Khan, Hunter College Student President and also on the trip pointed out, the rubber bullet could have been lethal "because we all know that rubber bullets are lethal, too."</P>
<P>Like the murder of activist Rachel Corrie --crushed to death by an Israeli bulldozer--the shooting of Nasser highlights the everyday brutality of Israel's war on Palestinians.</P>

