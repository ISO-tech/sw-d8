<P><font face="Times New Roman, Times, serif" size="4">Report exposes Cheney's energy task force</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Crooks write the laws</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 12, 2003 | Page 4</font></p></P>

<P><font face="Times New Roman, Times, serif" size="3">IT'S OFFICIAL. The corporate crooks of the energy industry helped the political crooks of the Bush administration write energy policies for the U.S. government. According to a report from Congress' investigative agency, the General Accounting Office (GAO), Energy Secretary Spencer Abraham privately discussed White House policy "with chief executive officers of petroleum, electricity, nuclear, coal, chemical and natural gas companies, among others."</P>
<P>Vice President Dick Cheney's energy task force, according to the report, went straight to "petroleum, coal, nuclear, natural gas, electricity industry representatives and lobbyists" for outside advice--and largely ignored the opinions of academic experts, environmentalists and policy groups.</P>
<P>Still, the extent of corporate involvement in coming up with the pro-business energy policies of the Bush administration may never be fully known. That's because Cheney's office has been refusing to turn over key papers to the GAO for two years now.</P>
<P>After a long battle in the courts, Cheney's staff gave up just 77 pages of documentation--two-thirds of which contained no cost information, and the remaining third included "miscellaneous information of little or no usefulness," according to the GAO. Known participants at meetings of the Cheney task force told GAO investigators that they "could not recollect whether official rosters or minutes were kept."</P>
<P>But the GAO did discover that task force meetings included private sessions with Ken Lay, the former head of Enron, the Texas energy company that bilked thousands of workers out of their savings as it plunged plunged into bankruptcy at the end of 2001. The GAO report also says that several corporations and industry groups, including Chevron and the National Mining Association, gave detailed energy policy "recommendations"--most of which were adopted wholesale by the Bush gang.</P>
<P>Among the policy guidelines the White House developed from the meetings: tax breaks and other incentives for energy production, including oil drilling in the Arctic National Wildlife Refuge. The task force was one of the biggest scams that the Bush administration had going when it took over the White House. No wonder they've tried to hush up the truth ever since.</P>

