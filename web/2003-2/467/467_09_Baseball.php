<P><font face="Times New Roman, Times, serif" size="5"><b>Baseball's ugly secret in Latin America</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Peter DiLeo</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 12, 2003 | Page 9</font></p></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">Arturo J. Marcana Guevara and David Fidler, <I><B>Stealing Lives: The Globalization of Baseball and the Tragic Story of Alexis Quiroz.</B></I> Indiana University Press, 2003, 280 pages, $27.95.</P>

<P><font face="Times New Roman, Times, serif" size="3">IN AN industry where revenues exceed $3.5 billion annually and the average player's salary is $2.5 million, it's hard to imagine anyone using the word "sweatshop." Yet that is the reality for thousands of Dominican and Venezuelan boys in their quest to reach "las Grandes Ligas."</P>
<I><P>Stealing Lives </I>chronicles Major League Baseball teams' systematic abuse of Latin American teens in their efforts to find the next Sammy Sosa. Each year, hundreds of boys, aged 15 to 19, sign meager contracts--many illegally--and are sent to so-called "baseball academies" in the Dominican Republic and Venezuela.</P>
<I><P>Stealing Lives</I> follows the story of Alexis Quiroz. Signed as a teen by the Chicago Cubs in Venezuela in the late 1990s, Quiroz was jerked around for several years, never given a copy of his contracts, paid less than he was told and, worst of all, sent to the Cub's medieval "academy" in the Dominican Republic.</P>
<P>With the carrot of Major League riches dangled in front of them, players practiced and played games for eight to 12 hours a day in sweltering heat with no water. Alexi lived in a house that crowded 10 players to a room, without running water. Their daily meals amounted to two small bowls of plantains and rice.</P>
<P>Alexis' nightmarish story culminates in a second tour at the academy where he dislocates his shoulder. The team--unwilling to pay for a proper medical attention--brings him to a utility shed where a "doctor" stomps on his shoulder to pop it into place. Not only was Quiroz never able to play again, but never regained full use of his arm.</P>
<P>This compared to the treatment of baseball prospects in the U.S. and Asia, where amateurs are showered with million-dollar signing bonuses and given the best training regiments. <I>Stealing Lives</I> is a shocking indictment of the depths that major league franchises will sink to make money.</P>

