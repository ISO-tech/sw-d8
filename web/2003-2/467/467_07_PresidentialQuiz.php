<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Time to take the presidential quiz</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 12, 2003 | Page 7</font></p></P>


<P><font face="Times New Roman, Times, serif" size="3">TEST YOUR knowledge of U.S. party politics with this short quiz. (Hint: the answer is not "Bill Clinton.")</P>
<P><I>Question:</I> Which former president spearheaded legislation making contraception widely available?<br>
<I>Answer: </I>George H.W. Bush, as a congressman, in 1967.</P>
<P><I>Question:</I> Which former president proposed a universal health care plan far more ambitious than 2004 Democratic presidential contenders Howard Dean, Richard Gephardt or John Kerry?<br>
<I>Answer:</I> Richard M. Nixon.</P>
<P><I>Question:</I> Which former president recently spoke in favor of legalizing gay civil unions?<br>
<I>Answer:</I> Gerald Ford.</P>

<P>These answers may surprise people accustomed to George W. Bush's neo-conservative brand of Republican Party politics. But on a range of issues, mainstream Republicans from the 1970s were far more "liberal" than mainstream Democrats today.</P>
<P>Washington is chockfull of career politicians who have changed their views on issues such as abortion, gay rights and racial equality. Most describe their lurches left and right as matters of "personal conscience"--as Bush Sr. did in shifting from a pro- to anti-abortion stance.</P>
<P>In reality, such shifts are typically the result of vote-searching, not soul-searching. With their fingers to the wind, politicians of both major parties readily bend to the moral values of their perceived voting base and financial backers.</P>
<P>In the late 1960s and early 1970s, at the peak of the women's and civil rights movements, mainstream politicians from both parties acknowledged some of the movements' demands.</P>
<P>With the decline of the movements and the rise of Reaganism in the 1980s, the winds began to shift--and politicians collectively scrambled to reinvent themselves by promoting the "family values" of conservatives. Bush Sr.'s conversion on abortion coincided with his ascendancy as Ronald Reagan's running mate.</P>
<P>The Democratic Party also shifted rightward in the 1980s. In 1972, Democratic presidential candidate George McGovern ran on a platform to end the Vietnam War, redistribute national income and construct a national health care system. But McGovern was the last liberal candidate of the Democratic Party.</P>
<P>After Nixon's landslide victory, the Democrats embarked on a strategy to win back Republican voters. In 1985, conservatives inside the party launched the Democratic Leadership Council (DLC), to promote candidates who would answer to big business, not to the party's traditional voting base of liberals, labor, women and Blacks.</P>
<P>Clinton stole the Republican Party's thunder on themes such as "personal responsibility"--while barely paying lip service to liberals, whose votes he took for granted. Clinton promised a business-friendly universal health care plan (which he didn't deliver) and welfare repeal (which he did). Clinton's 1996 welfare reform bill--eliminating 61-year-old New Deal legislation entitling the poorest of the poor to income support--was an attack Reagan wouldn't have dared.</P>
<P>Now the political winds are shifting again, and some Democrats have noted the rise of the antiwar movement and growing backlash against Bush's reactionary social policies. Democrats Howard Dean and Dennis Kucinich--after opposing the war on Iraq--are running for president with the support of many on the left. But their supporters should be wary.</P>
<P>Kucinich's platform is farthest to the left, but he has virtually no chance of winning the party's nomination. And he has only recently undergone a "personal journey" distancing himself from his own conservative past. In 1998, Kucinich voted for Clinton's "Iraq Liberation Act," promoting regime change in Iraq, and he still calls for a "strong and efficient military." His reversal of decades of staunch opposition to women's right to choose roughly coincided with the launch of his presidential campaign.</P>
<P>Dean's campaign has garnered the support of liberals across the U.S. Yet he has taken great pains to reject the liberal label, stating recently, "I was a triangulator before Clinton was a triangulator. In my soul, I'm a moderate." In the unlikely event that Dean should win the party's nomination, his would be a short slide into the arms of the DLC.</P>
<P>One thing is certain: next November, the Democrats will not be running a left-wing candidate against Bush. The growing opposition to Bush can aim much higher than supporting another repackaged Republican with Democratic Party credentials.</P>

