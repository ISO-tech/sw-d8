<P><font face="Times New Roman, Times, serif" size="5"><b>Bush's handouts to corporate polluters</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 12, 2003 | Page 4</font></p>

<P><font face="Times New Roman, Times, serif" size="3">THE BUSH administration used the late summer lull of press attention in Washington to stuff more money in Corporate America's pockets. That's the net effect of a series of changes in environmental regulations that will make it easier on corporate polluters. The "quiet flurry of late-summer activity [delivered] almost every rule change on Corporate America's wish list," reported the mainstream Knight-Ridder news service.</P>
<P>Among the restrictions loosened were those governing air pollution from old coal-fired power plants; emissions that cause global warming; ballast water on ships contaminated with foreign species of plants and animals; and drilling for oil and gas on federal land. The Bush gang even managed to ditch a regulation preventing the sales of land tainted with PCBs--one of the most toxic substances on earth.</P>
<P>In every case, big business got exactly what it wanted. And the bosses aren't keeping quiet about it, either. "We certainly had a number of victories this week; I don't think anyone can deny that," smirked Bill Kovacs, vice president for environmental issues for the U.S. Chamber of Commerce. Kovacs and other lobbyists interviewed by Knight-Ridder said the Bush administration had delivered almost every environmental regulatory change that Corporate America put on its to-do list in January 2001. </P>
<P>Even some Republicans are taken aback. "There's a lot of dramatic change going on," said Dan Esty, the deputy chief of staff for the Environmental Protection Agency during the presidency of George Bush Sr. "And a good bit of which would be thought of by many as not very environmentally sound."</P>


