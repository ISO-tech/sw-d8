<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 12, 2003 | Pages 10 and 11</font></p>

<P><p><font face="Times New Roman, Times, serif" size="4"><b>Hexcel</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Steve Leigh</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">KENT, Wash.--More than 230 members of International Association of Machinists and  Aerospace Workers (IAM) District 160 struck a Boeing subcontractor Hexcel on August 21. The company wants to raise the employee contribution to health care to 30 percent and, even worse, it's demanding the right to decide what medical, dental and prescription plans will be available to employees. This could raise costs out of the reach of the workers.</P>
<P>Hexcel is offering workers a 2 percent wage increase over the life of the contract. The company also wants to weaken recall rights and gain the right to unlimited subcontracting. According to strikers, Hexcel's real goal is busting the union.</P>
<P>The company has hired security guards to harass the pickets. And management is calling laid-off workers and threatening them with loss of unemployment benefits unless they scab--a violation of Washington state law. They are also pressuring nearby businesses to prevent pickets from parking there.</P>
<P>Hexcel is a worldwide company, and the plant in Kent is its only unionized one. Hundreds of workers in Kent have been laid off in the last few years. Hexcel has turned down work, rather than increase its unionized work force.</P>
<P>The strikers are getting solidarity from other unions. Teamsters respect their picket lines. Garbage workers won' t cross the picket line, so trash is piling up. Other Boeing unions, such as IAM District 751 and the Society of Professional Engineering Employees in Aerospace, are pledging support.</P>

<p><font face="Times New Roman, Times, serif" size="4"><b>Long Island University faculty</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Lucy Herschel</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">BROOKVILLE, N.Y.--More than 300 faculty at Long Island University's (LIU) C.W. Post Campus went on strike on September 8. They joined some 500 faculty who walked out at LIU's Brooklyn campus on September 3.</P>
<P>The main issues in the strike are heath benefits and condition for adjunct professors. The university wants to require that newly hired full-time professor pay 50 percent of the cost of family heath care coverage. Adjunct professors, who teach 65 percent of LIU's classes, don't receive any paid medical benefits from the school.</P>
<P>At the same time, LIU has raised tuition 7 percent and is building a $10 million gymnasium. "The full-timers have been supportive of the part timers," Professor Melissa Antinori, a union negotiating committee member, <I>told Socialist Worker. </I>"The adjunct issue was a definitely a strike issue for us. We're fighting for the kinds of conditions that will allow faculty to be the best teachers they can be--and that means support for research and support for all faculty, part-time and full-time, to earn a living wage and the health benefits they need." </P>
<P>Members of the Professional Staff Caucus, representing the staff of the City University of New York system, have been on hand at the picket lines to offer solidarity. On Orientation Day, Teamster drivers delivering food refused to cross the picket line.</P>

