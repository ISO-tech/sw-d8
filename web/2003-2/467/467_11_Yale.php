<font face="Times New Roman, Times, serif" size="5"><b>Strikers at Yale fight on</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sam Bernstein</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 12, 2003 | Page 11</font></p></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW HAVEN, Conn.-- Following the second week of a strike that began August 27, about 2,500 office, dining hall and maintenance workers are continuing to pressure Yale University. Chanting, "No contract--no work, no peace!" more than 600 workers, students and community members picketed and protested in front of Yale University President Richard Levin's house last Thursday afternoon. They spoiled a reception to celebrate a new concert hall that will be maintained and run by subcontracted nonunion labor.</P>
<P>Locals 34 and 35 of Hotel Employees and Restaurant Employees (HERE) are struggling to win better wages, health care, pensions and job security. In addition, the 150 dietary workers at Yale-New Haven hospital, members of SEIU District 1199, are on strike for a contract and the right to organize other Yale health care workers.</P>
<P>While the unions and the corporation resumed negotiations with New Haven Mayor John DeStefano, Yale has refused to budge on the key issues. Despite an $11 billion endowment, the Yale Corporation claims that it cannot afford to sufficiently increase its pension fund, which has a surplus of $200 million.  </P>
<P>Under Yale's proposal, the average worker with 30 years on the job would receive only $900-1,000 a month after retiring, some as little as $330. The unions demand that this be increased to $1,500 a month.</P>
<P>Another major sticking point is retroactive pay. Since their contracts expired in January 2002, some workers have lost up to $6,000 in back pay--and Yale is now proposing 50 percent retroactive pay for every person.</P>
<P>While negotiations continue to falter, the unions are dealing a blow to Yale. On September 4, the professor of a large lecture class was forced to cut class short due to the noise of boisterous picketers outside the hall. And by the second week of classes, some 160 professors have moved their courses off campus to churches, restaurants, city hall or the local movie theater in order to honor picket lines.</P>
<P>Although the initial "big bang" that accompanied the beginning of the strike is receding and the workers are feeling the pinch of living on $150 a week in strike benefits, the morale and excitement continues to be great. On September 3, more than 200 strikers rallied outside of the courthouse in support of 80 workers who were arrested in a civil disobedience action at the outset of the walkout.</P>
<P>The striking unions are also organizing a region-wide labor march and rally September 13, busing in thousands of workers from New England and New York. As Sally Miller, who has worked in the admissions office for 18 years and is a member of Local 34, told <I>Socialist Worker</I>, "Yale hasn't given us anything.  We have fought for everything we have."</P>


