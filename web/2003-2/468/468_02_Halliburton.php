<P><font face="Times New Roman, Times, serif" size="4">Haliburton lines its pockets</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Cheney's old pals rake in contracts</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 19, 2003 | Page 2</P>

<P><font face="Times New Roman, Times, serif" size="3">THE CROOKS at Dick Cheney's old company, Halliburton, had more on their minds than "nation-building" when they set their sights on Pentagon contracts to "rebuild" Iraq. Cold hard cash.</P>
<P>According to the <I>Washington Post, </I>Halliburton--where Cheney was CEO until he took over as the Bush gang's behind-the-scenes head puppet master--has raked in more than $1.7 billion in contracts to "rebuild" Iraq. And it stands to make hundreds of millions more under a no-bid contract awarded by the U.S. Army Corps of Engineers.</P>
<P>Halliburton is the closest thing to a household name for Army personnel in Iraq. Through its subsidiary Brown and Root, it has subcontracted to do everything from build and manage military bases to deliver mail and make meals. Halliburton also builds prisoner of war camps. Then there's the $705 million that the company got paid for working on getting Iraq's oil fields running again.</P>
<P>Cheney is responsible for Halliburton's cozy relationship with the Pentagon. After serving as defense secretary under George Bush Sr., Cheney headed to Halliburton when he got drummed out of office--and he turned the company's attentions to Pentagon construction contracts, just as military officials began putting out more and more work to private companies.</P>
<P>Today, Halliburton is only the best-connected bird in the group of vultures hovering over Iraq. Others include the California-based giant Bechtel, which won hundreds of millions in U.S. Agency for International Development reconstruction contracts--and Virginia-based DynCorp, which is training the new Iraqi police force.</P>
<P>As much as one-third of the $3.9 billion monthly cost of keeping U.S. troops in Iraq is going to independent contractors, according to estimates. It's time to expose the Washington war makers for their real aims--getting rich off the misery and suffering of millions of Iraqis.</P>


