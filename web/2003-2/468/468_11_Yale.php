<P><font face="Times New Roman, Times, serif" size="4">10,000 rally in support of strikers at Yale University</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"We're speaking up"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Meredith Kalman</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 19, 2003 | Page 11</P>

<P><font face="Times New Roman, Times, serif" size="3">NEW HAVEN, Conn.--The labor movement descended on Yale University September 13 to teach to teach Yale President Richard Levin a thing or two about solidarity. More than 10,000 Yale strikers, students and union members from across the country took part in one of the biggest demonstrations in New Haven's history. After blocking a downtown intersection, 152 demonstrators were arrested.</P>
<P>Among the unions represented were UNITE, United Brotherhood of Carpenters, Service Employees International Union, United Steel Workers of America, Communications Workers of America and the Teamsters. Many student solidarity groups from universities across the country also came for the protest.</P>
<P>For many, the rally was about more than the contract battle between Yale and members of Hotel Employees and Restaurant Employees. It was about economic justice in Bush's America. </P>
<P>The day before the rally, undergraduate solidarity activists organized a teach-in to mobilize student support for the strike. HERE Local 35 member Shirley Lawrence spoke on the panel and remarked on the significance of the strike. "Entire generations have been working for Yale," Lawrence said. "This institution was built on slavery. My mother just retired from Yale. Everyone I know at one point or another has worked for Yale. And we have gotten very little in return. We have become quiet for a long time about what we need. We are finally speaking up."</P>
<P>Yale is New Haven's biggest employer. With the decline of manufacturing jobs, Yale has come to employ 28 percent of the city's working population--compared to the 7 percent employed in manufacturing. </P>
<P>Professor Michael Denning, who also spoke on the panel, says that the Yale struggle represents the start of the new labor movement in America. "The issues of this strike will follow everywhere you go in America," said Denning. "This is a new phase for the labor movement in representing the rights of people in the service sector." </P>
<P>The strike, combined with demonstrations and active picket lines, has pushed Yale to concede on some key demands. Yale has publicly announced to pay 50 percent of retroactive pay increases and has agreed to the union's job security language. The unions, however, want full retroactive pay. Higher pensions are also a key demand of the unions, with 3 percent of the annual payroll devoted to pensions, while Yale is offering only 1 percent.</P>
<P>Community activists also recently won a seat at the bargaining table. They are demanding that Yale provide more scholarships to children of Yale workers attending college and make Yale's education more accessible to the New Haven community. Yale only pays $2.1 million in "voluntary contributions" to the city. Due to an archaic 19th century law, Yale is tax-exempt--and is robbing New Haven from $40 million in property taxes each year. </P>
<P>Morale is high on the picket lines, and workers feel a historic significance in their strike. "We are making history right here, by standing up and fighting," said Lawrence. </P>
<P>Last week, Yale brought in undocumented Latino workers as part of their scab operation as a conscious effort to turn Black workers on the picket lines against them. There was an initial level of hostility and racist slurs were thrown around at the scabs.</P>
<P>But a group of workers decided instead to engage the Latino workers, and the unions brought in Hispanic clergy to talk to the scabbing workers. In the end, 13 of them ended up joining the picket lines. </P>
<P>Now is the time to step up the pressure with more action. Yale needs to realize that, without the union, Yale can't operate.</P>


