<P><font face="Times New Roman, Times, serif" size="5"><b>NYC's Harvey Milk School deserves support</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 19, 2003 | Page 4

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker: </B></I><br>
While I understand the concerns raised by Matt Hanson in his letter "A step forward for gay teens?" (<I>SW</I>, August 22) that a high school for primarily gay students would lead to further isolation and allow "homophobia to flourish" in the rest of the New York City public school system, I disagree with his conclusion that we should not support the school. With its recent expansion, the Harvey Milk School (HMS) will go from serving 50 kids to as many as 170 students. Its specific mission is to meet the needs of gay, lesbian, bisexual and transgender (GLBT) students. </P>
<P>The school has become a target for right-wing nut jobs such as Rev. Fred Phelps, who brought his homophobic zealots to New York City to protest the school's opening. Longtime homophobe state Sen. Ruben Diaz and the right-wing Liberty Counsel filed a lawsuit against the school for "discrimination."</P>
<P>Even if it is solely on the basis of protecting gay (or straight) students' right to attend an openly pro-gay school in the face of this right-wing assault, we should support this school being able to keep its doors open. </P>
<P>But there is more to the argument. Hanson is right that this school will not solve the problem of homophobia, but it does not make sense to counterpose the existence of HMS to the fight to free all schools from homophobia.</P>
<P>In addition to the 70 percent of GLBT youth who are subjected to harassment or violence in school, studies show that up to 40 percent of GLTB youth attempt suicide. The majority of HMS students are poor, 75 percent are non-white, and 10 percent are in foster care. Last year's HMS valedictorian lived in a group home after his family abandoned him because he was gay. For gay kids whose parents deem them "sick," or who are getting beaten daily at school, why not allow them a school where they are actually treated like human beings?  </P>
<P>Let's put the fight for gay rights where it belongs--on everyone's shoulders--and not prevent gay kids from having a tiny haven in the midst of that fight. HMS in no way solves the problem of homophobia, and it is only by fighting for a safe and affirming environment in all schools--through things like straight-gay alliances, sane sex education, teachers' unions, etc.--that we will actually begin to overcome homophobia. </P>
<P>But there is no need to counterpose the overall fight against homophobia to supporting the existence of a school oriented to gay students.</P>

<B><P>Meredith Kolodner, </B>New York City</P>


