<P><font face="Times New Roman, Times, serif" size="4">More compensation for CEOs who rip off workers</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Rewarding the job cutters</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 19, 2003 | Page 4

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker: </B></I><br>
One common reason given for why socialism is "unrealistic" is the old line that "without profits, nothing would ever get done." Economic disparity, we are told, is the engine that drives our society by rewarding "beneficial" behavior with big payoffs.</P>
<P>Well, the Institute for Policy Studies (IPS) has released its 2003 version of "Executive Excess," a joint project with United for a Fair Economy, that details trends in CEO performance and compensation. Capitalism's defenders have been proven partially right. CEOs <I>are</I> rewarded with big payoffs--for ripping off their workers and squeezing every dime they can out of their companies!</P>
<P>According to the report, "Median CEO pay at the 50 companies with the most layoffs in 2001 rose 44 percent from 2001 to 2002, while overall CEO pay climbed only 6 percent...At the 24 Fortune 500 companies with the largest number of subsidiaries in offshore tax havens, median CEO pay over the 200-2002 period was...87 percent more than the median three-year pay at 365 corporations surveyed by <I>Business Week.</I>"</P>
<P>Their facts are sharp--but their conclusions weren't. IPS and United for a Fair Economy lay the blame for these galling statistics with Congress for not passing legislation that would require companies to account for stock options for executives like any other form of compensation, and for not passing other reforms to make corporate bosses "play fair." </P>
<P>But adjusting the rules won't help when the whole point of the game is to accumulate as much profit as possible, and not to provide for society's needs. There's no doubt about who wins and who loses. In the last 20 years, the ratio of CEO pay to worker pay has increased from 42-to-1 to 282-to-1! The system <I>is</I> working for those who own it: the ruling class.</P>
<P>Now more than ever, with the 2004 election season beginning, we need to be firm but patient with the many people who want to stop the machine from grinding up more and more workers by electing Democrats. We must make it clear that real change comes from mass movements from below, not legislation from above--and that it's unrealistic to place any hope in simply "changing the players." Regular people will continue to be ripped off and used up until we change the game itself!</P>

<B><P>Renee Collins, </B>Washington, D.C.</P>

