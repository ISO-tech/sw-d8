<P><font face="Times New Roman, Times, serif" size="4">Senate votes to stop new rules from going into effect</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bush's overtime scam stopped</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 19, 2003 | Page 2</P>

<P><font face="Times New Roman, Times, serif" size="3">THE BUSH administration's plan to boost employers' profits by ending overtime pay for millions of workers has hit the skids--at least temporarily. Early this year, the White House announced sweeping changes to the 1938 Fair Labor Standards Act--the law that guarantees time-and-a-half pay for workers who work more than 40 hours a week, with the exception of administrative, professional and executive workers.</P>
<P>But the rule changes have provoked such outrage that the normally timid Democrats in Congress took action last week. A few Republicans even jumped ship, and the Senate voted 54 to 45 to block the White House from issuing the new rules.</P>
<P>The administration claims that 1.3 million more low-income workers would be eligible for overtime under their revised rules. There's one problem, say White House critics--most of these workers already qualify for overtime to begin with.</P>
<P>And what the Bush administration won't admit is that their plan would also allow employers to more easily reclassify millions of employees into "exempt" categories. More than 8 million workers could lose their overtime rights--and be forced to work extra hours without extra compensation.</P>
<P>Under the new rules, any worker who earns more than $425 a week, supervises two or more employees a week and has a small amount of training or experience beyond high school would no longer qualify for overtime rights. Paralegals, emergency medical technicians, licensed practical nurses, surveyors, reporters, technical writers, bookkeepers, dental hygenists, lab technicians, cooks and assistant managers at retail stores are just a few of the people who could see their eligibility disappear. Even some blue-collar autoworkers, carpenters, construction workers and others could be affected--if they perform non-manual work during at least part of their workweek they could be labeled as "professional."</P>
<P>"The administration's proposals would create, in effect, a massive subsidy to employers paid for by their employees," said Ross Eisenbrey, vice president of the Economic Policy Institute, in a statement. "As more employers take advantage of the new rules, it will create a rush-to-the-bottom pressure that will eventually force even reluctant employees to participate in order to keep their labor costs competitive."</P>
<P>With Bush threatening to veto the Senate ban on imposing the new rules, it's clearer than ever where Washington's real priorities lie--helping Corporate America make their employees work harder and longer for less.</P>


