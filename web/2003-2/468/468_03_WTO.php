<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Dictating terms to the world's poor</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 19, 2003 | Page 3

<P><font face="Times New Roman, Times, serif" size="3">THE COLLAPSE of World Trade Organization (WTO) talks in Canc&uacute;n, Mexico highlighted the crisis of corporate globalization--and the ruthless determination of the U.S., Europe and Japan to squeeze poor countries harder than ever anyway. With thousands of demonstrators kept away by police barricades and warships surrounding the exclusive resort, delegates sat down to negotiations that were expected to hinge on the reduction of government subsidies to agribusiness in the world's rich countries.</P>
<P>But representatives from 22 developing countries--led by Brazil and India, and backed by China--walked out when officials from the European Union (EU) and the U.S. declared that they would only reduce such subsidies if developing countries removed restrictions on investment by multinational corporations. The message of the world's most powerful countries was blunt: We'll let you sell a bit more of your agricultural products in our markets, but only if you allow our corporations to dominate your economies even more.</P>
<P>As former World Bank economist-turned-globalization critic Joseph Stiglitz put it: "The strategy that the U.S., and to a lesser extent Europe, seems to be following is the usual: hard bargaining, extreme positions, last-minute concessions, arm twisting, peer pressure, tacit threats of cutting off development assistance and other benefits, and secret meetings among a small number of participants are all designed to extract concessions from the weakest."</P>
<P>The well-heeled representatives of the countries that walked out on the WTO don't share the same agenda as the protesters in the streets, however. Their governments are angling for a better niche within the system, not to overthrow it--and they'll keep negotiating in the quiet of the WTO headquarters in Switzerland.</P>
<P>For the mass of the world's poor, however, the situation is a matter of life and death. According to the International Food Policy Research Institute, poor farmers in Asia, Latin America and Africa lose $24 billion a year because of rich-country handouts to corporate agricultural giants.</P>
<P>In the U.S. alone, "farm subsidies" will be a $180 billion giveaway to the likes of ConAgra and Archer Daniels Midland over the next decade. The result is that farmers in poor countries find their markets swamped by cheaper imports from wealthy countries. That's what drove South Korean farmer Lee Kyung-Hae to publicly commit suicide during a protest in Canc&uacute;n.</P>
<P>Outrage over such conditions fueled the rise of the global justice movement that reached the U.S. with the mass protest at the 1999 WTO summit in Seattle. Since then, continued protests and a world recession--including the economic collapse of free trade-oriented Argentina--have forced trade officials in wealthy countries to talk about reform and "development." But while the U.S. had finally yielded to pressure in allowing patented AIDS drugs to be sold cheaply in poor countries, Washington is using its concessions to camouflage discredited free-trade policies.</P>
<P>Meanwhile, the U.S. has stitched up bilateral trade deals with Singapore and Chile and has pursued talks on similar deals with Australia, Morocco and South Africa. So even if there's a deadlock in the WTO--where Washington has repeatedly battled Europe in recent years--the U.S. is proceeding to create an economic bloc at the expense of its European and Japanese rivals. To that end, Washington is stepping up its pursuit of the Free Trade Area of the Americas (FTAA)--an attempt to extend the North American Free Trade Agreement from the Arctic Circle to the southern tip of South America.</P>
<P>But the protests that dogged negotiators in Canc&uacute;n will be there again in Miami for the FTAA summit on November 20-21. The message will be the same: We don't want your free-trade economic model--and another world is possible.</P>


