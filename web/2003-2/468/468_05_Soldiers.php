<P><font face="Times New Roman, Times, serif" size="5"><b>Soldiers' crisis looms for the Pentagon</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 19, 2003 | Page 5</P>

<P><font face="Times New Roman, Times, serif" size="3">DICK CHENEY and Donald Rumsfeld appeared on last Sunday's TV talk shows to insist that the U.S. isn't "bogged down" in a Vietnam-style "quagmire" in Iraq. But Rumsfeld missed an opportunity during his trip to Iraq earlier this month to send his message to U.S. troops stationed in Tikrit.</P>
<P>And no wonder. The super-hawk defense secretary was apparently worried that he would be booed off the stage by soldiers who have been in the region for six months--and fear that they won't come home until next March and beyond. "I don't give a damn about Rumsfeld," Specialist Rue Gretton, one of the soldiers in Tikrit, told Reuters. "All I give a damn about is going home. The only thing his visit meant for us was we had to clean up a lot of mess to make the place look pretty."</P>
<P>U.S. soldiers were told that they would be welcomed as liberators in Iraq. But four months after George W. Bush declared "combat operations" in Iraq over, American troops are facing regular guerrilla attacks, while carrying out an occupation for oil and empire that has no end in sight.</P>
<P>Since Bush's announcement on May 1, at least 154 U.S. soldiers have died in Iraq--more than were killed during the invasion. More than 6,000 service members have been evacuated from Iraq for "medical reasons" since the beginning of the war--including more than 1,500 who were wounded in combat.</P>
<P>Meanwhile, the Bush administration and its friends in Congress have only just been shamed into extending a temporary increase in soldiers' pay for combat operations and separation from their families. All this is leading to growing bitterness among soldiers.</P>
<P>"The problem is, after the hostilities ended, we were treated like dirt," Dennis Baumis, a reservist with the 1st Battalion, 293rd Infantry, wrote in a letter to an Indiana newspaper earlier this month. "We were swept under the rug and forgotten...Every time we complained through our 'chain of command,' the reply was always the same: 'Suck it up and drive on.'"</P>
<P>The anger among ordinary soldiers highlights a problem for Washington's war makers--they can't keep expecting anyone to sign up with the regular military or the reserves when it has become clear that they will be thrown into life-threatening situations at the whim of the White House.</P>
<P>Last week, Specialist Matt Drish told a Reuters reporter that he remembered how he and other soldiers were rallied by a Bush speech in January about Saddam's "weapons of mass destruction." Now Drish thinks Bush was talking about "weapons of mass distraction...They haven't found none," he says, "so I think this war's about just one thing--oil." </P>



