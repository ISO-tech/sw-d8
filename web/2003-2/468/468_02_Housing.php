<P><font face="Times New Roman, Times, serif" size="4">Rents grow out of reach for working poor</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Unaffordable Housing</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 19, 2003 | Page 2 </P>

<P><font face="Times New Roman, Times, serif" size="3">THERE'S NOT a single state in the entire nation where people with low incomes can find affordable housing. That's the verdict of "Out of Reach 2003," a new report from the National Low Income Housing Coalition.</P>
<P>According to the report, in 40 states, workers have to earn more than twice the minimum wage in order to be able to reasonably afford a one- or two-bedroom unit. In California, Massachusetts, New Jersey, New York, Maryland and Connecticut, they must make at least three times the minimum wage.</P>
<P>The report calculates the national "housing wage"--the amount a person working full time has to earn to be able to afford a two-bedroom rental for no more than 30 percent of their total income. In 1999, the housing wage across the country was $11.08 an hour. Today, the national housing wage is $15.21 an hour--nearly three times the federal minimum wage for a two-bedroom apartment.</P>
<P>"When low-income families are paying so much of their income on housing, they are left to skimp on other necessities like food, medicine, clothes and time spent with children," Sheila Crowley, president of the National Low Income Housing Coalition, told the <I>New York Times</I>. Worse, those who can't make their rent often have nowhere left to go.</P>
<P>According to a 2001 report by the National Coalition for the Homeless, for example, 42 percent of all homeless people are employed in full- or part-time jobs. "It is appalling that here in America, the richest country in the world, we have millions of people working full-time, plus seniors and people with disabilities, who cannot afford decent, modest rental housing," Crowley said in a statement.</P>
<P>What is desperately needed is more housing subsidies for workers and the poor--and tougher rent control laws, particularly cities like New York and San Francisco with outrageously high rental prices. But if Congress gets its way, as many as 114,000 low-income families may be thrown out on the streets next year. </P>
<P>In its proposed appropriations bill, Republican leaders of the House shortchanged the federal housing voucher program for poor families--known as Section 8--that pays the difference between the market rent of an apartment and 30 percent of a household's income. No one knows for certain how many people will get kicked out in the cold.</P>


