<P><font face="Times New Roman, Times, serif" size="5"><b>A DNA deadline in Florida</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 19, 2003 | Page 2

<P><font face="Times New Roman, Times, serif" size="3">PRISONERS IN Florida are in a race against time as an October 1 deadline for DNA testing approaches. In 2001, state legislators passed a law giving inmates two years after their sentencing to request that a judge review DNA evidence which might prove their innocence. Anyone convicted before the law went into effect could also apply for testing--but they, too, had to meet the arbitrary October 1 deadline.</P>
<P>Now, hundred of inmates are scrambling to get their requests completed before October 1. If they don't, the law shamefully allows evidence in pre-2001 convictions to be destroyed if no request for testing has been filed by the deadline.</P>
<P>The law came two years ago in response to a high-profile case that exposed the failures of the Florida death penalty system. DNA testing proved the innocence of Frank Lee Smith--convicted of a 1985 murder and rape of an 8-year-old.</P>
<P>But the truth came out too late--a year after Smith died on death row from cancer.</P>
<P>Smith isn't alone--in January, Rudolph Holton, who served 16 years on death row, became the 25th person to be exonerated in Florida.</P>
<P>As October 1 approaches, the Florida Supreme Court is considering an appeal to extend the deadline. Earlier this month, the Florida Bar Association's rules committee voted 23-10 to file an emergency petition with the Florida Supreme Court requesting an extension of the rule to October 1, 2004. And prosecutors in Broward County--where Frank Lee Smith was wrongfully convicted--have announced that they will continue to conduct DNA testing beyond October 1.</P>
<P>Broward State's Attorney Mike Satz became the first state prosecutor who said he would not prevent inmates from getting the tests if they miss the deadline. When even <I>prosecutors </I>are embarrassed at how the state's death penalty laws have failed the innocent, then it's time to look at abolishing capital punishment in Florida altogether.</P>


