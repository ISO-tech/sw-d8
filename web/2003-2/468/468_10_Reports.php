<P><p><font face="Times New Roman, Times, serif" size="4"><b>Stop the WTO</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Joe Cleffie</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Nearly 2,000 people marched on September 13<SUP> </SUP>to protest the World Trade Organization (WTO) meetings in Cancun, Mexico. The protest, organized by the coalition United for Peace and Justice, was one of more than 60 solidarity events that happened over the weekend in the United States.</P>
<P>The march from Union Square through Greenwich Village and SoHo ended in Washington Square where a combination of speeches and political music denounced the WTO for its policies that rob people all over the globe of things like medicines and food. "It's great to see the global justice movement finally coming back," said one activist. "We lost a lot of momentum after 9/11, but the conditions that people face and the policies of the WTO are the same if not worse. We knew it was only a matter of time."</P>
<P>A number of people new to the global justice movement turned out--many who'd attended their first protest in the winter to protest the war on Iraq. "It seems like it's the same people who benefit from the war on Iraq and from the WTO's trade policies," said one woman. "That's why I'm here." Marchers stopped along the way at companies like ExxonMobile, one of the WTO's biggest boosters.</P>

