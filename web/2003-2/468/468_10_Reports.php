<p><a name="Asscroft"></a><font face="Times New Roman, Times, serif" size="4"><b>Protest John Ashcroft</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Annie Levin and David Thurston</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">IN EARLY September, Attorney General John Ashcroft embarked on a multi-city tour to hype the civil-liberties-bashing USA PATRIOT Act--and to push for even more Big Brother law enforcement provisions. But everywhere he went, he was greeted with loud opposition.</P>
<P>On September 9, more than 1,000 protesters confronted Ashcroft in Boston. Ashcroft had to nerve to tell the small audience of hand-picked police officers and politicians, that he was honored "to talk about freedom in a setting like this."</P>
<P>But according to the <I>Boston Herald</I>, the noise of the protesters rang inside the hall during his speech. Protester Riaz Ahmed told the <I>Herald</I> that he came to protest after the Treasury Department threatened to cancel his bank-approved mortgage--because of his "Middle Eastern-sounding" name. "I'm not especially liberal or conservative but I think this is a pretty egregious infringement on people's rights," he said.</P>
<P>Ashcroft was greeted by another group of furious protesters in New York City on the same day, when as many as 1,000 made their way through police barricades of downtown Manhattan at a protest organized by United for Peace and Justice and the NYCLU's Bill of Rights Defense Campaign. Chants of "Ashcroft, go home! Leave our Bill of Rights alone!" echoed from buildings in the financial district. </P>
<P>Many protesters where particularly angry about the Bush administration's racist use of the war on terrorism to scapegoat immigrants. "The war on terror is a convenient excuse to go forward with a racist, anti-immigrant agenda," Arthie from the immigrant rights group Families for Freedom, told <I>Socialist Worker</I>. She pointed out that between 1996 and 2003, more than 1 million people were deported from the U.S.</P>
<P>"Immigrants and families are seeing their rights eroded," Lee Gough of the Brooklyn War Resisters' League told <I>Socialist Worker.</I> "That shouldn't be what this country's about." </P>

