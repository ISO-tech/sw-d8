<p><a name="NYU"></a><font face="Times New Roman, Times, serif" size="4"><b>New York University</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Wrigley-Field</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--New York University (NYU) students returned to school to find that the university administration has begun implementing restrictive new policies against student clubs. Clubs are now restricted to meeting just twice a month for two hours. </P>
<P>More appallingly, NYU has also begun charging student groups--including the office for gay, lesbian, bisexual and transgender students, and the office for students of color--for holding events in the student center.</P>
<P>NYU's excuse is a "space shortage." Yet last year's prime student performance space is now being rented out to Starbucks, and entire floors of the "student center" are frequently reserved for corporate conferences.</P>
<P>About 25 NYU students from more than a dozen organizations gathered recently to plan how to organize against these restrictions. A petition drive demanding unlimited meetings, club offices, priority over outside corporations, and more is in the works. Students are also planning on challenging NYU's president head-on at a Town Hall meeting on September 17.</P>



