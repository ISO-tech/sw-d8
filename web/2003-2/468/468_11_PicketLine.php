<p><a name="Hyatt"></a><font face="Times New Roman, Times, serif" size="4"><b>Grand Hyatt Hotel</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Nearly 1,000 members of the Hotel Employees and Restaurant Employees (HERE) Local 6 walked off the job August 22 at the Grand Hyatt Hotel to protest management's blatant indifference to their health and safety. The impromptu strike was a response to the hotel's refusal to obey an arbitrator's decision that it shut down a faulty cooling tower leaking hazardous chemicals and exposing workers to numerous long- and short-term health risks.</P>
<P>The Grand Hyatt's callous disregard for worker safety was so glaring that the arbitrator included a clause in his decision giving the union the right to take any action necessary if management didn't fully comply with the order. So when management refused to lift a finger, even as the deadline set by the arbitrator passed, union workers walked off the job and rallied in the lobby.</P>
<P>After realizing that they couldn't get a court in town to overturn the arbitrator's decision and facing a hotel full of guests, including the Baltimore Orioles baseball team, management caved at 6 p.m. They agreed to build a protective barrier around the areas where workers were being exposed to the toxins and completely fix the cooling system within the next 10 weeks.</P>
<P>The workers will also likely be paid for the time lost due to the stoppage. In light of the recent contract battle at Verizon, this victory shows that drawing the line and taking militant action can and does win.</P>

