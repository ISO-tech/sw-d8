<p><a name="Bon"></a><font face="Times New Roman, Times, serif" size="4"><b>Bon Macy's</b></font> <BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Steve Leigh</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEATTLE--By a vote of 72 percent, members of the United Food and Commercial Workers (UFCW) ended their five-month contract campaign against Bon Macy's. The contract held off some of the worst demands from Bon, but workers did accept concessions in the deal. In their "last and final offer," Bon management had gone for out-and-out wage cuts. </P>
<P>While the approved contract will not cut any current employee's wages, it does create a two-tier wage structure by bringing new employees in on a lower scale. The company had wanted health care costs to go up immediately, but the final contract delayed the cost increase until July 2004 when the employee share will jump to a whopping 50 percent! </P>
<P>There are no general wage increases in the three-year contract, but there are two one-time bonuses. This means that real wages after inflation will drop over the life of the contract. </P>
<P>The outcome of the campaign is unfortunate. In July, 70 percent of the workers covered by the contract voted to strike against the "last and final offer." A spirited rally of several hundred workers followed soon after. </P>
<P>But instead of using that momentum, the union leadership continued negotiations for two more months and squandered the opportunity to strike during the "back-to-school" sale in August. Union leaders knew the contract proposal was a setback, so they didn't formally endorse the offer, but in practice they argued for it at the ratification meeting. </P>
<P>The workers had only a couple hours to consider the contract before voting, so there was no time to mount an organized opposition. In spite of this, 28 percent opposed it. </P>
<P>This shows significant opposition--and a potential to organize a better contract campaign to reverse the concessions in the next contract. This will require strong rank-and-file organization next time.</P>


