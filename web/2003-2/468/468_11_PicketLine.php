<p><a name="Hospital"></a><font face="Times New Roman, Times, serif" size="4"><b>St. Peter Hospital</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Brian Huseby</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">PROVIDENCE--Health care workers at Providence St. Peter Hospital rallied in the rain September 10 to protest management delays in contract negotiations. A major sticking point is health care benefits for the licensed practical nurses, surgical technicians, certified nursing assistants, housekeepers, dietary workers and others whose contract expired in June.  </P>
<P>Management has refused to provide information about the benefits package or employee costs for the upcoming contract period. Without that information, the union bargaining committee can't move forward with wage negotiations.  </P>
<P>"They haven't provided us with the information we need," said Kelley Staley, a surgical technician and member of the Service Employees International Union Local 1199NW bargaining committee. "They may give us a great raise and take it away in benefits."</P>
<P>Staffing levels are also a major concern, and the union wants a joint labor-management committee to discuss staffing--a proposal flatly rejected by St. Peter management. "People are fired up and determined," Carter Wright of Local 1199NW told <I>Socialist Worker.</I> "[The workers] know their demands are reasonable."</P>

