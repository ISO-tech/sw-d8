<p><a name="LIU"></a><font face="Times New Roman, Times, serif" size="4"><b>Long Island University</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Wallis</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Faculty members at Long Island University voted to return to work, as <I>Socialist Worker </I>went to press. Strike coordinator Ralph Engelman said the concessions they had won constituted an important symbolic victory. The medical benefits for which some adjuncts will now be eligible, though meager, are "a foot in the door" toward winning the respect they deserve, Engelman told <I>Socialist Worker.</I> </P>
<P>After the administration threatened to take away strikers' medical benefits, members of New York State United Teachers Local 3998 held a rally in a nearby church where representatives of other trade unions joined them. "Corporate America will not stop until we stop them," said Stanley Aronowitz, relating the strike to the recent struggle at Verizon.</P>
<P>Aronowitz is chapter chair of the professors' union at City University of New York and former Green Party gubernatorial candidate.</P>
<P>Classes missed during the strike will not be made up, but tuition will also not be refunded to the students, even though salaries of strikers will be docked 3.4 percent this year. "The strike is over," Professor Engelman said, "but the struggle is not."</P>

