<P><p><font face="Times New Roman, Times, serif" size="4"><b>DaimlerChrysler</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">A TENTATIVE agreement between DaimlerChrysler and the United Auto Workers (UAW) was announced as <I>Socialist Worker</I> went to press. While details weren't immediately available, several press reports indicated that the deal includes an agreement to allow Chrysler to close or sell as many as seven parts plants.</P>
<P>This indicates that the UAW is continuing its policy of allowing the Big Three auto manufacturers to reduce costs by moving increasing amounts of production to parts plants with lower wages. The deal is the first in a series of negotiations that cover nearly 300,000 workers at Chrysler, Ford and General Motors as well as auto parts producers Delphi, formerly part of GM, and Visteon, a spinoff of Ford.</P>

