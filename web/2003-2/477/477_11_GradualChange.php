<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Is social change always gradual?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 21, 2003 | Page 11 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">WE'RE TAUGHT in school that change is something that happens gradually. Sudden, abrupt changes are seen as disruptions in a "normally" functioning society.</P>
<P>There's no doubt that "gradualness" is one feature of human society. For example, the minimum wage in the U.S. has gradually--very gradually--increased over the last decade. But to attribute all change as an accumulation of smaller, gradual changes flies in the face of reality.</P>
<P>But gradual change and big, qualitative leaps shouldn't be counterposed. They actually go together. At the turn of the last century, a number of countries "gradually" emerged as world economic powers, carving up colonies and "spheres of influence" in fierce competition with one another. The result of this development was two catastrophic world wars.</P>
<P>The Russian revolutionary Leon Trotsky put it well: "For us, it is enough to know that 'gradualness' in various spheres of life goes hand in hand with catastrophes, breaks and upward and downward leaps. The long process of competition between two states gradually prepares for war, the discontent of exploited workers gradually prepares a strike, the bad management of a bank gradually prepares a bankruptcy."</P>
<P>The ultimate social leap or break is revolution. For what is revolution but a period when the gradual accumulation of mass bitterness and anger of the exploited and oppressed coalesces and bursts forth into a mass movement to overturn existing social relations and replace them with new ones?</P>
<P>The last three centuries have been dotted not only with dozens of wars, but with revolutions, revolts and near-revolutions. To mention only some--the American Revolution of 1776, the French Revolution of 1789, the European revolutions of 1848, the U.S. Civil War of the 1860s, the Russian Revolutions of 1905 and 1917, the 1918-23 German Revolution, Spain in 1936, Hungary in 1956, Chile in 1973 and Poland in 1980--gives an idea of the scope of revolutionary upheaval since the dawn of modern capitalism.</P>
<P>A few days of revolutionary upheaval bring more change than decades of "normal" development. Rulers and systems that seemed invincible are suddenly unceremoniously toppled. For example, in November 1918, mass strikes of German workers and soldiers brought down German absolutism in a matter of a few days.</P>
<P>But revolutions don't come out of nowhere. No doubt, it was the gradual accumulation of anger and the gradual accumulation of experience in struggle and the confidence to fight--combined with the catastrophic effects of the First World War--that prepared German workers and soldiers for revolt.</P>
<P>But even short of revolution, not all change is gradual. Industrial unions didn't come to the U.S. by the gradual addition, year after year, of a few newly formed unions. On the contrary, mass industrial unionism came in an explosion of organizing and mass strikes over a period of about five years--from 1934 to 1938.</P>
<P>Revolution is not an aberration in an otherwise smoothly functioning society. It is the necessary means by which human society moves forward. Revolutionary force is, in Karl Marx's words, the "midwife of history"--necessary because no ruling class ever exited the stage of history without fighting to maintain its own rule and privileges.</P>
<P>Marx summed it up this way: At a given level of production, people enter into a set of social relations that correspond to it. At a certain point, however, those relations become a fetter on the further development of the productive forces of society. A revolution is necessary to burst those fetters and create a new basis for development.</P>
<P>Under capitalism, that contradiction is manifested periodically in the fact that the system goes into an economic crisis where it can't feed people or put them to work--that it produces vast wealth on the one hand and vast misery on the other. The only way to overcome this appalling contradiction is for the mass of workers to overturn capitalism--and build society anew on the basis of collective, socialized production and distribution.</P>

