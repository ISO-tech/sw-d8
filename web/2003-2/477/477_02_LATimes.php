<P><font face="Times New Roman, Times, serif" size="5"><b>Not a "resistance" to the <I>LA Times</I></b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 21, 2003 | Page 2 </font></P>

<I><P><font face="Times New Roman, Times, serif" size="3">THE LOS Angeles Times </I>has banned the phrase "resistance fighters" from its pages. At least when it refers to Iraqis.</P>
<P>The newspaper's assistant managing editor Melissa McCoy ordered reporters not to use the phrase to identify those waging attacks on U.S. forces in Iraq. McCoy said that she "felt the phrase evoked a certain feeling, that there was a certain romanticism or heroism to the resistance."</P>
<P>Instead, McCoy told reporters to use the terms "insurgents" or "guerrillas." Unsurprisingly, the Defense Department flatterers at the <I>New York Times </I>"agreed" with the <I>LA Times'</I> decision.</P>
<P>But <I>Washington Post </I>foreign editor David Hoffman said that he found nothing objectionable about using "resistance fighters" to describe Iraqi forces. "They are resisting an American occupation so it's not inaccurate," Hoffman said. "We try to be as precise as possible and distinguish, whether they are former Ba'ath party, Fedayeen, outsiders, insiders. But that's not always possible."</P>
<P>The <I>LA Times' </I>obsession with proving its pro-war credentials makes you wonder what they'll do next. Will the paper insist on referring to Halliburton's oil contracts as "liberation contracts"?</P>


