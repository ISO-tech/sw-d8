<P><font face="Arial, Helvetica, sans-serif" size="2"><b>SRI LANKA</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Power struggle at the top could reignite a civil war</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Behind the crisis in Sri Lanka</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Ganesh Lal</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 21, 2003 | Page 6 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">A BATTLE for power among Sri Lanka's most powerful officials is threatening to open a new stage in the country's bloody civil war. On November 4, Sri Lankan President Chandrika Kumaratunga caused a constitutional crisis by imposing a state of emergency and stripping authority from key members of parliament.</P>
<P>Kumaratunga made his move while Prime Minister Ranil Wickremesinghe was in the U.S. meeting with Bush administration officials. Wickremesinghe hurriedly returned as Kumaratunga revoked fundamental constitutional rights and brought the military into the streets of Colombo, Sri Lanka's capital.</P>
<P>The crisis represents the escalation of a power struggle between the biggest political parties in Sri Lanka. The government is currently led by the United National Front (UNF), which includes Wickremesinghe's center-right United National Party (UNP), as well as the Sri Lanka Equal Society Party (LSSP) and the Communist Party, both of which claim to be workers' parties, but long ago abandoned socialist politics. The UNF is opposed in parliament by the People's Alliance, which is dominated by Kumaratunga's right-wing Sri Lanka Freedom Party (SLFP).</P>
<P>At the heart of the conflict is the latest developments in a "peace process" that is supposed to end with a power-sharing arrangement for Sri Lanka's Tamil-speaking Hindu Muslim minorities. The Tamils have faced discrimination and oppression at the hands of the Sinhala-speaking Buddhist majority ever since Sri Lanka, formerly known as Ceylon, gained its independence from British rule in 1948.</P>
<P>The main forces behind the oppression of Tamils have been fascist organizations led by militant Buddhist monks. At the political level, the SLFP has been the main beneficiary of vicious anti-Tamil pogroms carried out by the fascists in 1972 and 1983.</P>
<P>In response to the pogroms, the Tamil New Tigers was formed in 1972--and later became the Liberation Tigers of Tamil Eelam (LTTE). But the LTTE, far from leading a liberation struggle, is itself a ruthlessly power-hungry outfit that systematically murdered its rivals on the Tamil left.</P>
<P>Today, the LTTE has little support among ordinary Tamils--but it has a substantial paramilitary force and has maintained its position as "spokesperson" for the Tamil minority through brute force. Since the 1980s, a civil war between government forces and the LTTE has claimed more than 60,000 lives, with hundreds of thousands more injured and displaced, the majority of them Tamils.</P>
<P>In 2002, the government and the LTTE agreed to a ceasefire and a "peace process" mediated by Norway. Wickremesinghe's UNP backs the negotiations and wants a power-sharing arrangement with the LTTE. </P>
<P>The UNP's big business supporters hope that by ending the civil war, they can establish stability--in order to attract foreign capital and take advantage of Sri Lanka's cheap labor costs to produce for the world market. On the other hand, the hard-line Sinhala factions among Sri Lanka's rulers--such as Kumaratunga's SLFP and the Buddhist clergy--don't want a truce with the LTTE, and insist on maintaining Sinhala dominance over the country's affairs.</P>
<P>On October 31, the LTTE announced its plans to set up an interim self-governing administration in the north and east of the country, where Tamils predominate. In response, the SLFP joined forces with the Sinhala-chauvinist Janatha Vimukti Peramuna within the ruling coalition--and Kumaratunga imposed the state of emergency four days later.</P>
<P>For the millions of ordinary Sri Lankans, Sinhalese and Tamil alike, there is little to choose from between the various parties of the UNF government on the one hand and the LTTE on the other. A movement from below, uniting workers of all ethnic backgrounds, could challenge the priorities of a system that pits Tamils against Sinhalese in the interests of power and profits.</P>
<P>The potential for such a united workers' opposition did exist in the 1960s, but it was squandered when the LSSP, a formerly Trotskyist organization, joined forces with the right-wing SLFP in order to win elections. Only a revolutionary, anti-communalist workers' movement can pose a real alternative to Sri Lanka's current crisis. </P>



