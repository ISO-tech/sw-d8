<p><a name="Moron"></a><font face="Times New Roman, Times, serif" size="4"><b><I>SW</I> shouldn't use the term "moron"</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
In the article "The Democrats Duck Another Fight" (<I>SW,</I> November 7), Bush was referred to as "President Moron." While I certainly agree with the sentiment, as socialists interested in fighting for the liberation of all oppressed groups, we should refrain from the use of this word. </P>
<P>People with disabilities in America unnecessarily face an uphill battle. Often excluded from mainstream classrooms, youngsters with disabilities are often never even given a chance to learn at the same level as their peers. They fall behind peers and often never learn to develop normal relationships or become self-sufficient. </P>
<P>Students from poorer or largely minority school districts are even less likely to have good inclusive education programs. Because of their exclusion from society, people with disabilities are branded as substandard humans. They are labeled "morons", "retards" or "disabled." </P>
<P>As socialists we should see people with disabilities for who they are: people who happen to have a disability and have been stigmatized for it and whose degree of inclusion into society depends on his or her socioeconomic status and race. The use of the word "moron" in any context further stigmatizes and stereotypes people with disabilities. </P>
<P>We should refrain from using this derogatory term as an insult, and we should fight for the full inclusion (with adequate assistance) of people with disabilities into general classrooms and their inclusion into broader society. </P>
<B>
<P>Bill Linville,</B> Madison, Wis.</P>



