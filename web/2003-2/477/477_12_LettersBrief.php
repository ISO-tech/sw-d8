<P><p><font face="Times New Roman, Times, serif" size="4"><b>Why the Greens should run</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I was recently lucky to be part of discussions with more than 60 Green Party activists from around the country who gathered in New York City to discuss whether the Greens should run a national presidential candidate. The activists were members of the Green Party USA--the left "anti-capitalist" wing of the Green Party. </P>
<P>Panelists spoke at the event from a number of different perspectives. There was a representative of the campaign of Democratic presidential hopeful Dennis Kucinich; an activist who argued that Greens should not run a candidate and instead focus on local campaigns; and others who argued strongly for a green presidential campaign. </P>
<P>About half of the audience, as well as some of the panelists, felt that Kucinich was worth supporting, at least until the Democratic primaries are over. Some felt that it was a "question of survival for the planet" to defeat Bush's re-election, and others said that Bush represented a fascist cabal that had begun to take power.</P>
<P>Other panelists, including myself, argued that it was crucial for an activist movement to have a candidate independent of the Democrats and Republicans. Don Fitz, from the magazine <I>Synthesis/Regeneration</I>, reminded the audience that "Nixon was the most progressive president in the last 30 years"--because pressure from below pushed Nixon to enact reforms.</P>
<P>Green Party activists around the country have a crucial decision to make over the next couple of months. People are looking to them on this question. To wait to put forward a candidate, and instead put energy into the Kucinich or Dean campaigns, will actually weaken the movement nationally for an alternative independent of both parties--the only thing that will ever force the politicians to do what we want. </P>

<B><P>Peter Lamphere, </B>New York City</P>

