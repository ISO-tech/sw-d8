<p><a name="Gonzalez"></a><font face="Times New Roman, Times, serif" size="4"><b><I>SW </I>misrepresented Gonzalez's position</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
It was not a mistake, as <I>SW</I> reported, for San Francisco Green Party mayoral candidate Matt Gonzalez to support the salary raise for members of the San Francisco Board of Supervisors (<I>SW,</I> November 14). The voters of San Francisco passed a ballot measure in favor of making being on the board a full-time position, with salary set by the civil service board.</P>
<P>Before this happened, supervisors either were rich, worked a second job that often brought up conflicts of interest (and their time), or both. Gonzalez's wealthy opponent Gavin Newsom has cynically used this issue. I'm sorry <I>Socialist Worker</I> fell for his line.</P>
<B>
<P>Steve Rhodes, </B>from the Internet</P>

