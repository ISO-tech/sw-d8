<p><a name="Shoppers"></a><font face="Times New Roman, Times, serif" size="4"><b>Taking to the aisles to build solidarity</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
In the heat of the Southern California grocery strike, supporters of the grocery workers have begun to use a new tactic to make "business as usual" more difficult for scabs and the grocery bosses. Members of the International Socialist Organization and other labor supporters engaged in what has come to be called "disruptive shopping." </P>
<P>After informing the picketers that we were entering the store, about a dozen or so activists filled up shopping baskets to the brim and then simultaneously occupied each checkout stand. After the goods were rung up, activists began to inform shoppers that the company was robbing workers of their health benefits while making millions in profit. </P>
<P>After several minutes, we all began to chant "Health care for all" and walked out of the store, where we were joined by the picketers. In unison, we continued to chant. We then walked the picket lines and engaged workers in various discussions using <I>Socialist Worker</I>, from the tactics of the strike to the occupation of Iraq and the alternatives to the capitalist system. </P>
<P>The sidewalk in front of the store was turned into an impromptu political forum. As one sign read, "This strike is for all of us." We should find ways to build solidarity with the strikers, as well as engage them in discussions about the fight for a better world.</P>
<B>
<P>Justin Akers,</B> San Diego</P>

