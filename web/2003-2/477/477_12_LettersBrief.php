<p><a name="AIDS"></a><font face="Times New Roman, Times, serif" size="4"><b>The millions who die because of profit</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
On the weekend of October 3, I attended the Student Global AIDS Campaign (SGAC) Leadership Retreat at Wellesley College outside of Boston. SGAC is a national student-led organization comprised of over 40 campus chapters and almost 100 affiliates. We are dedicated to fighting AIDS throughout the world through advocacy work. </P>
<P>As of this year, 25 million people have died of AIDS, and 100 million are infected with HIV, the virus that causes AIDS. Every day, nearly 8,500 people die of AIDS, and 15,000 people become infected. There is no reason these statistics should be so high. </P>
<P>Treatment can be made available for less than $1 a day, yet 95 percent of infected people worldwide do not have access to treatment. Instead of stopping the world's greatest medical crisis, pharmaceutical companies apply for 20-year patents, halting the possibility of inexpensive generic drugs for millions. And governments force Third World nations to repay substantial debt, while their people die and tens of millions of young children are orphaned. </P>
<P>AIDS is most certainly a disease driven by capitalist greed. America, always at the forefront of capitalism, has done almost nothing to help the millions affected by AIDS. </P>
<P>In his State of the Union speech, President Bush promised to spend $15 billion over the next five years to fight the pandemic. To date, he has given $2 billion, and refuses to pay any more. The U.S. is more than willing to spend $87 billion to set up an oil empire in the Middle East, but it cannot give a small $15 billion to help stop global AIDS. </P>
<P>The SGAC is working to save millions of lives, but anyone who despises the greed that fuels AIDS must join the fight. It is possible to stop AIDS, even as the pandemic is getting worse. We must demand aid for all infected people, and we must save millions of lives. </P>

<B><P>Kristina Elliott,</B> from the Internet</P>

