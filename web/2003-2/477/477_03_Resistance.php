<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">New strikes and struggles show sentiment for a fightback</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The signs of resistance</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 21, 2003 | Page 3 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">"I AM not employed at any grocery store, but I totally support the strike...Many of you say, 'So what? I have to pay for part of my benefits; it won't kill them to pay too.' Maybe not, but who's going to be next on the chopping block?...Somebody needs to stand up and say enough is enough...We may not be CEOs, but we deserve our fair share!" Signed Janet Hoover, Garden Grove, Calif.</P>
<P>Hoover's letter to the <I>Los Angeles Times </I>published in mid-November is a perfect expression of the growing frustration of millions of people with corporations that ensure their bottom line by making workers pay. That frustration has burst through in a number of strikes across the country.</P>
<P>In Chicago, sanitation workers ended their nine-day walkout in mid-October with a decisive win. In Connecticut, clerical and maintenance workers at Yale University defied a powerful administration with a strike that won important gains. In New York City, 800 workers at the city's ritziest restaurants beat back attempts to cut their health care and pension benefits.</P>
<P>The biggest battle, though, is the Southern California grocery workers--where more than 70,000 are entering their sixth week on strike or lockout in a fight against employer demands to raise workers' contributions to their health insurance. Even the <I>New York Times </I>recognizes what is at stake. In an editorial titled "The Wal-Martization of America," the <I>Times </I>wrote that the grocery battle "is, at bottom, about the ability of retail workers to earn wages that keep their families out of poverty."</P>
<P>None in the latest series of union struggles have come in the labor movement's traditional strongholds in heavy industry. The United Auto Workers, for example, negotiated a contract with the Big Three automakers in September that was filled with more concessions than any deal since the 1980s.</P>
<P>Instead, workers in weaker sectors, like the grocery workers represented by the United of Food and Commercial Workers (UFCW), found themselves on the front lines of the attempt to push back the employers' assault. On November 10, thousands of longshoremen backed them up, stopping work to attend a support rally for the strikers.</P>
<P>These are fights that union leaders, including those at the top of the UFCW bureaucracy, have tried to avoid. But the grocery workers in California recognize that the stakes are too high to ignore.</P>
<P>The growing political polarization in the U.S. has not only been expressed by recent strikes, but in attitudes about national politics as well. According to recent CBS opinion polls, the Bush administration's approval ratings were split on every issue--foreign policy, Iraq and the economy.</P>
<P>When asked whether removing Saddam Hussein was worth the loss of American lives, 50 percent said yes, and 43 percent said no. When offered a choice between Bush and an unnamed Democratic candidate in the coming presidential election, 41 percent of registered voters chose Bush; 43 percent went for the Democrat.</P>
<P>What's important about these polls is not simply that they show polarized opinions about the Bush agenda, but that they reflect a developing opposition to Bush--that people are feeling forced to take a side. If the Democrats bothered to recognize this fact, they might take up some of the fights that workers cared about. But they haven't. Instead, they are gearing up for what will probably be the most expensive election season ever.</P>
<P>In the absence of decisive action by either Democratic Party politicians or labor leaders, workers are beginning to fill the vacuum themselves--with struggle. These fightbacks may not be large, and they may not be reported in the mainstream media.</P>
<P>But struggles are taking place, around a range of issues, across the country--an expression of the anger and frustration felt by millions of people throughout the U.S. Building solidarity for these strikes and struggles wherever they arise can help to build the confidence of wider numbers of people to turn their anger into action.</P>


