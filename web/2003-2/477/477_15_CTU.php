<P><font face="Times New Roman, Times, serif" size="4">Problems remain in new deal for Chicago teachers</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>CTU votes on new contract</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jesse Sharkey, Delegate, CTU</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 21, 2003 | Page 15 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--Chicago teachers were set to vote on a tentative contract agreement as <I>Socialist Worker </I>went to press. The Chicago Teachers Union (CTU) House of Delegates gave a reluctant "yes" to the agreement on November 14 by a vote of 337 to 275.</P>
<P>The deal was negotiated just as the union was preparing for a strike-authorization vote. Rank-and-file teachers rejected an earlier tentative agreement--in spite of a concerted campaign to sell it by union leaders.</P>
<P>In the three weeks between the rejection of the first offer and the scheduled strike vote, the CTU finally showed signs of life--creating phone trees, making picket signs and organizing informational pickets. The union also reached out to community organizations and scheduled outreach to parents.</P>
<P>This contract battle came in the wake of a significant victory for Chicago garbage collectors and raised the possibility of another high-profile strike. And the rejection of the tentative agreement last month showed a great deal of on the part of the rank and file--especially considering that the deal was recommended by CTU President Deborah Lynch, who was elected as a reformer.</P>
<P>But less than a week before the planned strike vote, the union's negotiating team announced "victory" at the bargaining table. In the media, Lynch trumpeted the new agreement as a total win. "Our membership asked us to bring home the bacon, and we brought home the whole hog," Lynch told reporters.</P>
<P>At the House of Delegates meeting, each member of the negotiating team gave a short, rally-like speech celebrating some aspect of the deal. But the meeting showed that there is a gulf between the leadership and the vast majority of delegates.</P>
<P>Apart from 50 or so clapping supporters, most of the 700 people sat and listened to the 25-minute presentation in complete silence. Though the school board made several concessions, a substantial number of rank and filers are still unsatisfied.</P>
<P>The new deal will last four years, increase health care costs and do little to address many crucial concerns, such as improving working conditions and reducing class sizes. Unfortunately, many teachers will probably vote for this second offer because they see little alternative--nor do they relish the prospect of walking out with a divided union and a leadership that clearly doesn't want to strike.</P>
<P>With enough time and more rank-and-file organization in place, this offer had an excellent chance of being defeated. As it stands, the outcome remained in doubt as <I>Socialist Worker </I>went to press.</P>
<P>But the rejection of the first tentative agreement shows how angry and frustrated Chicago teachers are--and the potential for mobilizing a fighting rank and file. That will be the job in the months to come for teachers who want to turn their union around.</P>


