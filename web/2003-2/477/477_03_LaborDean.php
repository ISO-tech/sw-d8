<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Is Howard Dean a friend of labor?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 21, 2003 | Page 3 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">IS HOWARD Dean the candidate of the labor movement? Last week, Dean's campaign for the Democratic presidential nomination got a big boost when he won a joint endorsement from two of the country's most powerful unions: the Service Employees International Union (SEIU) and the American Federation of State, County and Municipal Employees (AFSCME).</P>
<P>Until then, Rep. Dick Gephardt (D-Mo.) was considered organized labor's choice. Gephardt has the backing of 21 mostly industrial unions, including the United Steel Workers of America and the International Brotherhood of Teamsters. Though his record is less consistent than advertised, Gephardt does have the reputation of voting with the unions in Congress, especially on trade issues, such as opposing the North American Free Trade Agreement (NAFTA).</P>
<P>So what does Dean have to offer labor? Not clear. On NAFTA, for example, Dean followed the lead of Bill Clinton and supported the free trade deal.</P>
<P>At last week's press conference, SEIU President Andy Stern praised Dean's proposals for health care reform. But Dean's program is more conservative than Gephardt's--not to mention the liberal dark-horse candidates for the nomination, such as Rep. Dennis Kucinich (D-Ohio) and Rev. Al Sharpton.</P>
<P>In fact, Stern and AFSCME chief Gerald McEntee openly stated last week that their primary concern in making an endorsement was "electability." That meant picking whatever Democrat seems best positioned to beat George Bush.</P>
<P>In Dean's case, the big reason is because he's raised so much campaign cash that he can skip out of the federal funding system. What Dean actually thinks about the issues that concern workers doesn't enter the equation.</P>
<P>Actually, the tussle over union endorsements obscures the real question--of whether <I>any </I>Democrat deserves labor's support. The Democratic Party's claim to be the party of working people dates back to the 1930s, when the newly organized industrial unions failed to follow through on strong sentiment to form a labor party--and tied their fortunes to Franklin Roosevelt and the Democrats.</P>
<P>Roosevelt is given credit for the New Deal programs and labor rights enacted during his time in office. But these reforms were surrendered by Roosevelt--under pressure from massive struggles. Ever since the 1930s, Democrats have been able to take organized labor's support for granted--even while stabbing unions in the back.</P>
<P>Bill Clinton's presidency was a perfect example. He campaigned for office promising to end 12 years of Republican attacks on working people.</P>
<P>After he won, he broke his promises--for example, doing nothing at all to win passage of anti-striker replacement legislation, even though Congress was controlled by Democrats. But when it came to Corporate America's NAFTA deal, he pulled out all the stops.</P>
<P>No one should expect anything else from Dean or Gephardt. Workers deserve better than this--a real political alternative, independent of Corporate America.</P>



