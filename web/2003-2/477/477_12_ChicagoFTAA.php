<P><font face="Times New Roman, Times, serif" size="5"><b>Labor has to take a stand to save union jobs</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 21, 2003 | Page 12 </font></P>

<P><font face="Times New Roman, Times, serif" size="3"><B>Dear <I>Socialist Worker,</B></I><br>
On November 1, I attended a rally sponsored by the Chicago branch of Jobs with Justice to protest the Free Trade Area of the Americas (FTAA). While the rally was reasonably well attended, it raised some troubling questions about how the labor movement addresses the issues surrounding the FTAA.</P>
<P>First, the site chosen for the rally was outside of the almost-closed Brach's Candy plant on the West Side of Chicago. Brach's was a longstanding Chicago employer that, at its height, had nearly 2,000 union employees, members of Teamsters Local 738. Brach's was a profitable company that received millions in tax breaks from the city to remain in Chicago throughout the 1990s. </P>
<P>Over a year ago, Brach's announced that it was going to phase out its operation in Chicago--not because it was losing money, but because it could make more money by moving its manufacturing operations to Mexico and Argentina! Teamsters Local 738 should have organized a vigorous labor-community fight to save the plant and the jobs. Instead, it did nothing.</P>
<P>The anti-FTAA rally was co-sponsored by Teamsters Joint Council 25 (the biggest in the country with 110,000 members) and Teamsters Local 738. How can we as a labor movement successfully fight these trade deals and relate to unions abroad, if the opportunity to fight over such an issue as the closing of Brach's is allowed to slip by, without even the whimper of a fightback?</P>
<P>Another focus of the rally was protesting the opening of a Wal-Mart store in the same neighborhood as the nearly closed Brach Candy. While the speakers rightly pointed out Wal-Mart's anti-union record, they ignored the fact that, in an extremely poor area like the West Side, many people would welcome both the jobs and low-cost products of Wal-Mart--particularly, since many of the Chicago-area retailers and supermarkets abandoned the West Side decades ago. The speakers should have just concentrated on the issue of organizing Wal-Mart employees, not on Wal-Mart's opening a store.</P>

<B><P>Joe Allen, </B>Teamsters Local 705, Chicago</P>


