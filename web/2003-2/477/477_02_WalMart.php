<P><font face="Times New Roman, Times, serif" size="4">Immigrant workers deprived of their rights</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The Wal-Mart sweatshop</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 21, 2003 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">"IT'S AN unlawful conspiracy to systematically deprive people of the most basic human rights." That's how lawyer James Linsey described Wal-Mart's treatment of thousands of immigrant workers on the left-wing radio program <I>Democracy Now!</P>
</I><P>In late October, federal immigration agents raided more than 60 Wal-Mart stores, arresting 250 undocumented workers employed as janitors by Wal-Mart subcontractors. As despicable as the government's scapegoating of these undocumented workers is, equally disgusting is the way that they were treated by Wal-Mart.</P>
<P>According to reports, Wal-Mart's janitorial subcontractors expected workers--mainly immigrants, both legal and undocumented--to work seven days a week, with just one or two days off a year. Pay was just barely above minimum wage. The workers were given no health or pension benefits and were cheated on overtime pay.</P>
<P>Yet if Wal-Mart--the largest and most profitable retailer in the U.S.--is found to have known about the workers' illegal status, the most it will face in the way of punishment is a fine of $10,000 per worker.</P>
<P>Now, however, several workers are fighting back with a class-action lawsuit charging that Wal-Mart knew about its subcontractors' labor violations. The suit demands that Wal-Mart and its contractors pay workers back overtime pay.</P>
<P>"This isn't happening overseas in some sneaker sweatshop in Asia," explained Linsey. "This is happening in our neighborhoods. One of our people--a diabetic, young guy, 18 or 19 years old, doesn't speak English--sliced his hand with the sharp blade that's used to strip wax off the floors. He was bleeding, and he was told to continue working, which he did. </P>
<P>"The next morning, when he got off work, of course, he didn't have any worker's compensation, no medical coverage, so he had to go to a hospital and literally beg to be served. The conditions are absolutely appalling." Wal-Mart management insists that it's done nothing wrong--and is trying to shift the blame onto "greedy lawyers."</P>
<P>"Clearly, hungry lawyers are converging on these illegal immigrants as if they were accident victims," Mona Williams, Wal-Mart's vice president for communications, whined to the <I>New York Times.</I> "If you are scrambling to make a buck at someone else's expense, who would you sue, an unknown cleaning contractor or the country's largest corporation?" But if anyone should know about "making a buck at someone else's expense," it's Wal-Mart--a company with a long history of union-busting and using sweatshop labor.</P>


