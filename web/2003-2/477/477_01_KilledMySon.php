<P><font face="Times New Roman, Times, serif" size="4">Mother of a U.S. soldier killed in Iraq:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"George Bush killed my son"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 21, 2003 | Page 1 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">THERE WERE no American flags at First Lt. Brian Slavenas' funeral. His mother, Rosemarie Dietz Slavenas, wouldn't allow them--or an honor guard or any other military trappings.</P>
<P>As she buried Brian--the pilot of the CH-47 Chinook helicopter shot down November 2 near the Iraqi town of Falluja--Rosemarie had a message for George W. Bush. "George Bush killed my son," she told reporters. "I request in Brian's name a stop to the killing. No more pre-emptive wars."</P>
<P>Donald Rumsfeld can't muster much sympathy for soldiers like Brian or his relatives. "In a long, hard war, we're going to have tragic days," the defense secretary said after the Chinook was shot down. "But they're necessary."</P>
<P>Necessary? Why? Because of Iraq's phantom "weapons of mass destruction" that the Bush administration lied about? </P>
<P>Because of Saddam Hussein's nonexistent ties to the September 11 hijackings? Because of the lie that U.S. troops would be "liberating" the Iraqi people?</P>
<P>Rumsfeld has some gall claiming that Brian's death was "necessary." "He was worth more to me than all the oil in Iraq," Rosemarie said in an interview with <I>Socialist Worker</I>.</P>
<P>Rosemarie speaks for a growing number of parents, spouses and children who don't want their loved ones used as cannon fodder in Washington's war for oil and empire. No wonder the Bush administration has tried its hardest to ignore the growing number of U.S. casualties--for example, banning press coverage of coffins returning from Iraq.</P>
<P>They won't even use the phrase "body bag" anymore--because it conjures up too many images of the U.S. defeat in Vietnam. Instead, today's dead soldiers are shipped home in what the Pentagon calls "transfer tubes." But changing the name won't hide the growing questions about what young men and women like Brian died for.</P>
<P>For the Iraqi people, the cost of the U.S. war has been many times higher. With its occupation unraveling in the face of increased resistance, Washington's answer has been to unleash even more brutality.</P>
<P>"Although the coalition can be benevolent, this is the same lethal instrument that removed the previous regime, and we will not hesitate to employ the appropriate levels of combat power," threatened Lt. Gen Ricardo Sanchez. In other words, the U.S. is going to the boot down even harder on ordinary Iraqis.</P>
<P>The name of its latest operation--"Operation Iron Hammer"--shows the intent. But worse than the name is what U.S. troops are doing in Iraq. Like when 2,000 troops from the 1st Armored Division--backed by tanks and helicopters--sealed off a 20-block area of Baghdad's Azamiyah district November 17, invading and searching every single building and arresting an unknown number of Iraqis. </P>
<P>Or when the U.S. dropped 500-pound bombs near Falluja and Tikrit in retaliation for attacks on U.S. troops--knowing full well that these bombs would kill and main innocent civilians. As Lt. Col. Steven Russell warned: "[Iraqis] need to understand that it's more than just Humvees that will be used against the resistance, and we will crush the resistance." </P>
<P>No wonder Washington's occupation only inspires more hatred. "If the Americans came as normal citizens, we'd welcome them," a man named Khalid told Britain's <I>Observer </I>newspaper. "When they came for liberation, I sent them food. Now I just want to kill them."</P>
<P>Washington's overseer in Iraq Paul Bremer told NBC's <I>Today Show, </I>"We will stay here until the job is done and until we have a democratic, stable Iraq." But the U.S. is the <I>cause </I>of instability in Iraq--and there's nothing "democratic" about the oil colony it is presiding over today. We have to stand up and say: End the occupation! Bring the troops home now!</P>



