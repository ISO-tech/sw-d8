<P><font face="Times New Roman, Times, serif" size="5"><b>They saved pennies while workers died</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Helen Redmond</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 21, 2003 | Page 11 </font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">David Von Drehle, <B><I>Triangle: The Fire That Changed America</B></I>. Atlantic Monthly Press, 2003, 352 pages, $25.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IN <I>TRIANGLE: The Fire That Changed America, </I>David Von Drehle takes readers step by step through the fire that took the lives of 146 immigrant women in the New York City garment district in 1911. Many of the workers--most of them young Jewish and Italian immigrants--were burned alive, and over 80 jumped to their deaths, in full view of the crowd assembled below.</P>
<P>Fire fighters didn't have ladders long enough to rescue workers, but most crucially, the doors were locked, preventing their escape. Why? To prevent employees from stealing.</P>
<P>During the trial afterward, Triangle owner Isaac Harris was asked to estimate the value of goods stolen in a year. "Ten dollars or fifteen dollars or twelve dollars or eight dollars, something like that," he replied. Harris and his partner were let off scot-free.</P>
<P>An exciting chapter titled "Uprising" sets the stage for the fire. The year before, 20,000 textile workers, including those at Triangle, went on strike to protest sweatshop conditions. Over 500 shops were hit by the strike, and more than 70 owners settled within 48 hours--but not Triangle.</P>
<P>Triangle bosses were determined to crush the union. Picket line violence, instigated by hired thugs and the police, occurred daily.</P>
<P>Clara Lemlich, one of the most famous strike leaders, had her ribs broken. It was Lemlich who stood up in a union meeting and made the motion for a general strike.</P>
<P>These passages showing workers' courage and sacrifice are exceptionally inspiring. <I>Triangle </I>touches on the alliance between workers and the rich society ladies who took up the strikers' cause--an odd coalition full of tension and contradictions--and goes into some detail about the role of socialists, but not enough.</P>
<P>The book would be more useful if it had focused more on the workers and their union--before and after the fire. Still, <I>Triangle </I>is a captivating book.</P>
<P>In every workplace fire, the memory of the Triangle workers lives on. Last month, six people died when a fire broke out in a Chicago office building. Survivors say the stairwell doors were locked. </P>



