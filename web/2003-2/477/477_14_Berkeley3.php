<P><font face="Times New Roman, Times, serif" size="5"><b>Defend the Berkeley 3</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 21, 2003 | Page 15 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">BERKELEY, Calif.--University of California-Berkeley Dean of Students Karen Kenney approved sanctions against three students for taking part in a peaceful campus sit-in on March 20, the day the U.S. began its "shock and awe" bombing campaign in Iraq. The Berkeley Stop the War coalition organized the protest, which drew 4,000 students to a rally and 400 to the sit-in.</P>
<P>Rachel Odes and Snehal Shingavi face 20 hours of community service and a letter of reprimand permanently placed on their academic record. Michael Smith faces 30 hours of community service plus a stayed suspension for one semester. </P>
<P>The administration also ordered Smith to submit to an insulting "anger management" course at the university's infirmary. If he is "successful," his suspension might be reduced to a letter of reprimand. Punishing political activists by sentencing them to psychological treatment invites comparison to the classification of dissent as a "psychiatric disorder" in Stalinist Russia.</P>
<P>Even more shocking, the administration pioneered the idea of "progressive discipline" to target the activists' mere association with protest. Shingavi, for example, was singled out for being the "point person" for a previous protest by the Students for Justice in Palestine--even though he was never arrested or charged in connection with that protest. This legal theory of "guilt by association" led the <I>Daily Californian </I>to editorialize that "by picking out only three, the message sent from the university seems to be that free speech includes the right to participate in a protest, but not the right to organize one."</P>
<P>With the dean's approval of the sanctions, the last appeal goes to Asst. Chancellor John Cummins, who will issue his final decision within 15 days. As the Bush administration carries out unprecedented attacks on hard won civil liberties, the Berkeley administration is shamefully jumping on the bandwagon. </P>
<P>We urge everyone who cares about free speech and the right to protest to take immediate action this week to demand that Cummins drop all charges against the Berkeley 3.</P>

