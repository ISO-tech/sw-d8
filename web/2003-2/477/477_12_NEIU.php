<P><font face="Times New Roman, Times, serif" size="4">Organizing against the anti-gay bigots in Chicago</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"We don't want your hate!"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 21, 2003 | Page 12 </font></P>

<P><font face="Times New Roman, Times, serif" size="3"><B>Dear <I>Socialist Worker,</I></B><br>
More than 100 students came out in solidarity for gay rights to protest Heterosexuals Organizing for a Moral Environment (HOME), an anti-gay hate group, on Northeastern Illinois University's (NEIU) campus November 11. The administration attempted to shut down the counterprotest--organized by the Socialist Club, the Gay, Lesbian, Bisexual Alliance, the Feminist Majority Leadership Alliance and the Sociology Club--by moving it to a secluded part of campus, behind closed doors.</P>
<P>But students held their ground and confronted the bigots head on, surrounding their table. For hours, students, faculty and entire classes joined the protest and challenged the bigots directly, chanting, "Two, four, six, eight, we don't want your anti-gay hate" and "Racists, bigots, anti-gay, take your hate and go away!" At one point, campus security even erected fences around the bigots! </P>
<P>While NEIU's administration has provided an open forum for bigots in the name of "free speech," it has repeatedly attempted to shut down the free speech of student groups organizing against the war and occupation in Iraq or holding anti-bigotry speakouts. This campus was also the scene of a hate crime this past semester, when an anonymous anti-gay, sexist letter was sent to the Women's Studies department.</P>
<P>The administration also provides security in the form of Illinois state police for hate groups like HOME, sending the message that hate is tolerable and equal rights are unimportant. HOME has a history of coming to NEIU's campus, but this is the first year that students have taken action against them.  </P>
<P>In a time when civil rights for are being attacked on all fronts, it is essential that we argue socialist politics sharply and patiently. It became evident that the administration was going to do nothing to protect gay rights on campus, and our politics and experience helped students take an active role and demand change through collective action--by directly confronting HOME.</P>
<P>Some students organizing against HOME on campus initially felt the struggle for gay rights should be left to the administration. But with the recent events and political arguments, students are now looking to create change by pressuring the administration and holding large actions. Change comes through struggle, and our politics can help shape how that struggle is carried forward.</P>

<B><P>Douglas Lee, </B>Chicago</P>

