<P><font face="Times New Roman, Times, serif" size="5"><b>Making the world look like Texas</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Cindy Beringer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 21, 2003 | Page 11 </font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">Molly Ivins and Lou Dubose, <B><I>Bushwhacked: Life in George Bush's America</I>. </B>Random House, 2003, 368 pages, $24.95.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">NOBODY KNOWS better than Texas humorist Molly Ivins the amount of damage possible when the man she calls "GeeDubya" is put in a chief executive position. We've been "Bushwhacked," says Ivins and co-author Lou Dubose in their book of the same name. In it, the writers show the painful human cost of  "the worst public policy created in Texas...gone national."</P>
<P>Bush's actions to enrich his inner circle at the expense of the rest of us are familiar, but the details revealed in the book's thorough research make for fascinating reading. We learn of a jobless recovery and shrinking unemployment benefits in Pennsylvania, racially predictive testing nightmares for the nation's school children, faith-based drug treatment facilities indicted for torture, green rabbits at superfund sites in New Jersey, fecal matter in meat in Pennsylvania and two chapters' worth of Enron shenanigans.</P>
<P>And there's much more--from the humorous to the horrifying. We learn how the early careers of both Bush and Bill Clinton intersect with rival chicken magnates Bo Pilgrim and Frank Tyson, who successfully lobbied for the right to pollute the water and mangle the hands of workers.</P>
<P>In <I>Bushwhacked,</I> Bush attends a men's Bible study group in Midland, Texas, to pray on the eve of the Iraq war with Rev. James Robison, an "anti-abortion rights fanatic given to quoting both sides of the conversations between him and God."</P>
<P> The Air Force plans to clean up the land under leaking containers of Agent Orange left over from the Vietnam war by shipping the substance to Brazil for use as a defoliant. U.S. State Department officer Elaine Jones horrifies attendees of an international conference on population control with her personal birth control plan involving checking cervical mucous. Bush's refusal to release millions in federal funds for a home heating program in Philadelphia results in several freezing deaths.</P>
<P>The book features an all-too-familiar cast of villains. They include Karl Rove, known to Bush as "Boy Genius" and "Turdblossom," former Enron CEO Kenneth Lay, and Priscilla Owens, Texas Supreme Court Justice and multiple appointee to the federal bench, who was literally "made" in Texas by the Bush family inner circle.</P>
<P>"Time to raise hell," Ivins observes correctly, but the only plan suggested is to vote for a Democrat to push Bush out of office. And in spite of all his enumerated faults, Clinton emerges as the book's dominant hero.</P>
<P>Long before Bush trashed Clinton's ergonomics rules, the women gutting 15 to 25 catfish an hour in the Mississippi Delta were making less than a living wage and arguing with the company for the right to bathroom breaks. They will, no doubt, continue to do so in the next Democratic administration.</P>

