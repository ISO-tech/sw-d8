<P><font face="Arial, Helvetica, sans-serif" size="2"><b>NO TO THE OCCUPATION OF IRAQ</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Getting ready for October 25</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 10, 2003 | Page 2

<P><font face="Times New Roman, Times, serif" size="3">"BRING THE troops home now!" Activists across the country are gearing up for October 25 demonstrations in Washington, D.C., and San Francisco that will raise that demand.</P>
<P>The protests will be the first national demonstrations against the occupation of Iraq since the war began. Supported by both United for Peace and Justice and International ANSWER--the two largest national antiwar organizations--October 25 represents an important step for the antiwar movement. We have the opportunity to turn the growing questioning of the Bush administration's war into an active show of opposition.</P>
<P>On many college campuses, students are using the Campus Antiwar Network's (CAN) "Speaking Truth to Empire" tour as a way of reaching a larger audience and building for the October 25 demonstration. In Chicago, activists at University of Illinois-Chicago and Northeastern Illinois University will host the CAN tour on October 16. Events will feature Military Families Speak Out member Lou Plummer, as well as family members of active-duty soldiers. </P>
<P>Antiwar activists at the University of Illinois have set up "voting booths" to ask students what they would like to see $87 billion spent on funding the occupation of Iraq, or increasing Pell Grants by 800 percent, or giving $300 to every man, woman and child in the U.S., or paying reparations to the Iraqi people. </P>
<P>In Boston, the Harvard Initiative for Peace and Justice is planning an anti-occupation rally on campus for October 21. The University of Maryland Peace Forum has two major events planned on campus in preparation for the October 25 march--one is a stop on the the CAN speaking tour, featuring activist Tariq Ali and other speakers. </P>
<P>Meanwhile, in Washington, D.C., members of the D.C. Anti-War Network (DAWN) are making the October 25 demonstration their top priority, handing out flyers for the event in the city and inviting people to get involved. On the West Coast, the Berkeley Stop the War Coalition will host the CAN tour on October 8, featuring Norman Solomon and Michael Parenti.</P>
<P>As one organizer said, "We will be making a pitch for anyone and everyone who is concerned about the occupation--and who wants to see October 25 be the biggest it can possibly be--to join the Berkeley Stop the War Coalition and help build the movement on campus and the October 25 protest." Following the tour stop, campus activists are planning a publicity blitz--including classroom announcements, a letter writing campaign to the student newspaper, and going door to door in dorms--to raise awareness about the demonstration. </P>
<P>Beginning October 15, students will begin a "10-day countdown" campaign, flyering each day with the top 10 reasons to oppose the occupation and come to the protest. A huge outpouring of antiwar protest gave the Bush administration a black eye prior to the war. Today, with the brutality of the U.S. occupation of Iraq on display for the world to see, it's even more important to take to the streets.</P>



