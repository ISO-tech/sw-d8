<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>How can anger be turned in action?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 10, 2003 | Page 7</P>

<P><font face="Times New Roman, Times, serif" size="3">"VOTERS DIRECT rage at their leaders," the <I>San Francisco Chronicle </I>reported on October 5, in an article describing widespread anger against both Democrats and Republicans in the California recall. Respondents in a <I>Chronicle </I>online opinion poll after the September 24 debate voted, not for Schwarzenegger, but Green Party candidate Peter Camejo, the winner of the debate, with 32 percent of the total votes cast. His "tax the rich" and antiwar message clearly struck a chord with many Californians.</P>
<P>Meanwhile, a <I>New York Times </I>poll released last week showed national discontent at its highest level since Bush took office. Just over four in 10 voters now hold a favorable opinion of Bush, and only 41 percent say the war in Iraq was worth the loss of life and other costs involved--while a 53 percent majority said it was not.</P>
<P>With this level of anger at government policies, it's only natural to wonder why the level of fightback is so low. Where are the mass protests?</P>
<P>Anger alone does not produce mass struggle--fightbacks do not emerge on a large scale simply because of a widespread sense of injustice.</P>
<P>It's easy to imagine the 1960s as a different era, when the mass of the population supported the goals of civil rights and opposition to war. But this is not the case--these movements were built over a period of years, by small groups of people with a strong sense of commitment and a long-term view of their goals.</P>
<P>Mass movements do not spring up overnight. Most people know that in 1955 Rosa Parks refused to give up her bus seat to a white man, sparking the Montgomery bus boycott that drew in tens of thousands of Black activists--and launching the mass civil rights movement that finally brought down legal segregation. But Parks was also thrown off a bus 11 years earlier--after refusing to enter through the back door--with no more than a ripple of protest.</P>
<P>The same dynamic is true of opposition to the Vietnam War. The first antiwar protest in 1964 in New York drew only 600 people. Eighty-three percent of the U.S. population supported the escalation of the bombing of North Vietnam in 1965.</P>
<P>But by late 1966, 100,000 demonstrated against the war, while 30,000 marched on the Pentagon, which was guarded by armed troops. By 1970, union leaders began to speak out against the war, and rank-and-file workers began joining in protests in larger numbers. By 1971, a Harris poll found a majority of Americans considered the Vietnam War "morally wrong."</P>
<P>The same is true of the struggle for justice in Iraq. Last winter, millions of people around the world demonstrated against war in Iraq--but for the years preceding, handfuls of activists built the small but determined movement against the sanctions on Iraq.</P>
<P>Changing circumstances cause people's ideas to change. The mass civil rights movement was a response to the post-war promise of an American Dream and prosperity for working class people--that systematically excluded African Americans because of segregation.</P>
<P>The Vietnam War was popular until it became clear that U.S. warmakers were content to send tens of thousands of working-class soldiers to their deaths for a war that met with sustained Vietnamese resistance--from the very people U.S. troops were meant to "save" from "Communism."</P>
<P>In these circumstances, widespread passivity gave way to principled determination to struggle for justice. And fear of fighting back was replaced by rising hope and expectations, as the movements grew.</P>
<P>Silence should not be mistaken for complacency today. Four in 10 of those polled last week by the <I>Times </I>were worried that someone in their household would lose their job in the next year--a measure of the state of consciousness within the U.S. working class.</P>
<P>The system is not working--as the riots against police brutality in Benton Harbor, Mich., last spring and the current sanitation strike in Chicago show on a small scale. Those who are fighting for justice today can pave the way for the larger struggles yet to come.</P>




