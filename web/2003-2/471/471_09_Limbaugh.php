<P><font face="Times New Roman, Times, serif" size="4">Rush Limbaugh forced to resign at ESPN</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Good riddance to a bigot</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Stuart Easterling</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 10, 2003 | Page 9</P>

<P><font face="Times New Roman, Times, serif" size="3">IF YOU thought you'd had enough of Rush Limbaugh, think again. He recently got a job on ESPN's Sunday football pre-game show, despite having no football background or experience. It seems that his only qualification was being a loudmouthed right-winger. Limbaugh soon embarrassed himself, declaring that the St. Louis Rams had never been to a Super Bowl under coach Mike Martz. Sorry Rush, they have--just two years ago.</P>
<P>ESPN's goal was to raise their ratings among the right-wing frat-boy crowd, and apparently to provide Rush with a platform to spew more of his bigoted garbage. They tried to pretend that the guy who once told a Black caller on his radio show to "take that bone out of your nose and call me back" could be counted on for insightful sports commentary.</P>
<P>In its most recent episode, Limbaugh decided to take on Philadelphia Eagles quarterback Donovan McNabb, who is Black. "I don't think he's been that good from the get-go," said Limbaugh. "I think what we've had here is a little social concern in the NFL. The media has been very desirous that a black quarterback do well--black coaches and black quarterbacks doing well. There is a little hope invested in McNabb, and he got a lot of credit for the performance of this team that he didn't deserve."</P>
<P>Unbelievable. McNabb has been a Pro Bowler for three straight years, and was the runner-up for the NFL's Most Valuable Player award last year. But according to Limbaugh, this is all just to due bias by the "liberal media" in favor of Blacks.</P>
<P>Said McNabb in response to Limbaugh's drivel: "It's sad that you've got to go to skin color. I thought we were through with that whole deal."</P>
<P>Limbaugh refused to back down, saying, "All this has become the tempest that it is because I must have been right about something." As usual, Limbaugh is long on rhetoric but short on facts.</P>
<P>He conveniently ignores how it took decades for Blacks to be accepted as quarterbacks in pro and college football. And how it was nearly impossible for African Americans to get a head coaching job in either college or the pros.</P>
<P>In the entire history of Division I college football, there have been only 20 Black head coaches. Today, there are only five (4.3 percent of the total), even thought 47 percent of the players are Black. In the NFL, there are only three Black head coaches out of 32 teams. Meanwhile, the management level in college and the pros is almost completely white. This is true in all sports, not just football.</P>
<P>Limbaugh finally had to resign due to the outcry over his comments. But ESPN defended him to the end, saying they didn't think his blather was racially biased. Who are they kidding? That racist bigmouth should have been fired and kicked off the air. But at least now his right-wing spew is off TV.</P>



