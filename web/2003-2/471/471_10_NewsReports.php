<P><font face="Times New Roman, Times, serif" size="5"><b>News and reports</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 10, 2003 | Page 10

<P><p><font face="Times New Roman, Times, serif" size="4"><b>No to the occupation of Iraq</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Potma, Adam Meyer and Steve Leigh</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">IN CITIES across the country, activists are continuing to build opposition to Washington's occupation of Iraq.</P>

<B><P><font face="Times New Roman, Times, serif" size="3">Boston</B><BR>
"From Iraq to Palestine, occupation is a crime!"chanted the 250 people that gathered September 28 in Boston to rally against occupation and empire. The protest, organized by International ANSWER, marched through downtown Boston, ending at the Israeli consulate to commemorate the third anniversary of the second Palestinian Intifada. </P>
<P>"The Iraqis have the right to self-determination," said Ahmed, a Palestinian activist, comparing the Iraqi fight against the U.S. occupation to the struggle for a free Palestine. "All the struggles are interrelated. We have to unite, we cannot separate our struggles," said Roberto, a Puerto Rican activist involved in the movement to kick the U.S. navy out of Vieques.</P>
<P>Kelly, a member of the Women's Fightback Network, agreed, stressing that "We have to combine our fightbacks against racism, sexism and class oppression with the struggles against war."</P>

<B><P>Austin, Texas<BR>
</B>The same day, hundreds of people from across Texas gathered in downtown Austin for a rally and march to say no to the U.S. occupation of Iraq. The event brought out veterans of earlier demonstrations, as well as many new faces fed up with Bush's lies. </P>
<P>Teri Allison of Military Families Speak Out (MFSO) spoke about the experience of having a loved one deployed as cannon fodder, and why she and others in MFSO are speaking up: "If you saw your son get into a car with a drunk driver, you wouldn't stand on the side of the road and salute, you would stop the car." She finished by reminding us that "We all have one common goal: to bring home our troops now!" </P>

<B><P>Seattle <BR>
</B>Five hundred opponents of U.S. intervention in Iraq marched several miles across Seattle October 5. Chanting "Black, Latino, Arab, Asian and white, no more war, no more war, defend our civil rights!" protesters marched to the Bureau of Immigration and Customs Enforcement (ICE) jail south of downtown. </P>
<P>In response, ICE detainees chanted back: "Let us out! Let us out!"--despite the fact that they had been threatened with being put in the basement if they responded to the demonstration. Sponsored by Not In Our Name, the loud, spirited protest laid the basis for more antiwar activity in Seattle--and for the upcoming October 25 national day of action in San Francisco and Washington, D.C.</P>

<P><p><font face="Times New Roman, Times, serif" size="4"><b>Stop racism at San Francisco State</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Sarah Levine</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--San Francisco State University has been the victim of yet another hate crime that could be swept under the rug if we don't take a stand. The Black Student Union sponsored a rally October 2 against a hate crime that took place approximately one month earlier. </P>
<P>Three African American women who shared a dorm room found ugly, hateful letters outside their door from a group calling itself Triple Threat. When the three women complained to their residence assistant, he told them that there wasn't anything he could do and threw the letters away. When the women complained to the housing office, their only answer was to offer to relocate them. </P>
<P>This isn't the first time we've witnessed these crimes. About a year ago, the Richard Oaks Multicultural Center was smeared with feces from wall to wall. Written in the feces were swastikas and racist pictures of African Americans.</P>
<P>The administration held a group meeting with student organizations, but nothing was done. Since then, racist and sexist pictures and writings have appeared elsewhere on campus, but no action was taken. </P>
<P>We can't wait for the administration to take care of this. The BSU rally was a good start to addressing the hate on campus, but all students need to be a part of the fight against racism.</P>




