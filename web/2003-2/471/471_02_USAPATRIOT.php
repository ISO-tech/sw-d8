<P><font face="Times New Roman, Times, serif" size="4">Feds use USA PATRIOT in hundreds of cases</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Grabbing new powers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 10, 2003 | Page 2</P>

<P><font face="Times New Roman, Times, serif" size="3">THE BUSH administration claimed that the civil liberties-shredding USA PATRIOT Act would help catch "terrorists." But the Feds have used the law and vastly expanded police powers to go after people suspected of crimes completely unrelated to terrorism, like drug trafficking, money laundering and other white-collar crime. </P>
<P>According to a Justice Department report, new powers granted under USA PATRIOT have been used in hundreds of non-terrorism cases. For example, the Bush administration has used the law to secure warrants to grab e-mail and other electronic evidence.</P>
<P>"What the Justice Department has really done is to get things put into the law that have been on prosecutors' wish lists for years," Elliot Mincberg, legal director of People for the American Way, told the <I>New York Times</I>. "They've used terrorism as a guise to expand law enforcement powers in areas totally unrelated to terrorism." Meanwhile, the administration is also continuing to use USA PATRIOT to go after political activists who they can now label "terrorists" on the flimsiest of evidence.</P>
<P>Last month, the Justice Department announced that it decided to revive a 16-year-old effort to deport two Palestinian activists, Khader Hamide and Michel Shehadeh--because as students, the two allegedly distributed magazines and raised funds for the Popular Front for the Liberation of Palestine (PFLP), a group that the government now labels a "terrorist" organization. The new attack on Hamide and Shehadeh comes despite several court rulings that deporting the two is unconstitutional because the men were not involved in terrorist activity.</P>
<P>Back in 1987, the Reagan administration attempted to deport Hamide, Shehadeh and six other Palestinian immigrants allegedly associated with the PFLP, by using a provision of the Cold War-era McCarran-Walter Act, which barred membership in communist groups. But a federal appeals court to declared the law unconstitutional, and Congress repealed it in 1990.</P>
<P>Then-FBI Director William Webster even admitted in 1987 that none of the eight had engaged in terrorist activity--and said that they would not have been arrested if they were U.S. citizens. Now, the Bush administration has decided to deport the men using a provision in the USA PATRIOT Act that bars giving material aid to organizations deemed by the government to be "terrorist."</P>
<P>Witch-hunts like these are exactly what the USA PATRIOT Act was designed to whip up--which is why more than 150 cities across the country have passed resolutions condemning it. But John Ashcroft doesn't want to hear it. On his recent cross-country "Victory" tour, the attorney general ridiculed opponents of the USA PATRIOT Act--including the American Library Association, which opposes the law's provision that allow government agents to rifle through patron records at libraries.</P>
<P>Ashcroft said that critics of the law are "hysterics"--and that "charges of abuse of power are ghosts unsupported by fact or example." The only people who should be worried about USA PATRIOT," he said, "are terrorists. If your idea of a vacation is two weeks in a terrorist training camp" or "if you enjoy swapping recipes for chemical weapons from your 'Joy of Jihad' cookbook," Ashcroft sneered to a Memphis audience, "you might be a target of the PATRIOT Act."</P>


