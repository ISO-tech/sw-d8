<P><font face="Times New Roman, Times, serif" size="5"><b>"Poor countries are systematically hurt"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 10, 2003 | Page 7

<P><font face="Times New Roman, Times, serif" size="3">THE WORLD Trade Organization (WTO) summit in Canc&uacute;n, Mexico, broke down after a walkout by developing countries, led by Brazil, India and China. These countries were protesting the refusal of the U.S. or the European Union (EU) to eliminate subsidies for their giant agricultural corporations. But the editors of the <I>Economist </I>magazine found a different scapegoat to blame the collapse of the summit on--the "globophobic" organizations and social movements that protested the WTO in Canc&uacute;n. The magazine singled out the views of Food First, the Oakland, Calif.-based research and activist group, as "scandalous rubbish." </P>
<P>LEE SUSTAR spoke to Food First co-director ANURADHA MITTAL about the collapse of the WTO summit.</P>

<P><B>WHY DID the <I>Economist </I>target Food First for blame?</B></P>

<P>WE WERE surprised to see the awkward neologism "globophobic" used to describe our organization and the international span of social movements and government representatives with whom we work. There is nothing globophobic in pointing out that poor countries and the farmers within them are systematically hurt by U.S. and EU agricultural policy. </P>
<P>The developing countries' obstinacy in Canc&uacute;n can be blamed for the outcome of the talks, but only if one ignores--as the EU and the U.S. have done throughout the WTO's agriculture negotiations--that most farmers in these countries have nothing left with which to bargain. </P>

<B><P>THE WTO meeting in Doha in 2001 was supposed to be the beginning of the "development round" of trade negotiations to benefit the Third World. What happened?</P>
</B>
<P>I THINK it's a myth that Doha was supposed to launch the development round. Let's not forget how it was accomplished. The meeting was right after September 11, when the message of "you're either with us or against us" became the backbone of U.S. foreign policy, which strained relationships with other countries. It was, "If you disagree with trade agreements, you are against us."</P>
<P>I wrote a piece with Walden Bello, "The Meaning of Doha," which outlined the true outcome of Doha, and it predicted that the growing discontent among the developing nations and the lack of transparency and democracy in the WTO would unravel the institution. </P>

<B><P>THE QUESTION of EU and U.S. agricultural subsidies was supposed to be on the table in Canc&uacute;n. Then, they demanded new agreements on investment from developing countries. How did this happen?</P>
</B>
<P>THE THIRD World countries had made it very obvious that they were not open to negotiations on the new issues. The most contentious issue was agriculture, which was offered to the Third World countries as the carrot to join the WTO. Instead of market access for their agricultural products, poor nations have seen the U.S. and the EU subsidize their agribusinesses to the tune of $1 billion a day, and they face dumping of cheap subsidized grains into their countries. </P>
<P>The draft for the summit endorsed the U.S. and EU agriculture proposal, basically dividing up the world among themselves for their markets, while completely ignoring the concerns and demands of the developing countries. So only the U.S. and the EU can be blamed for the failure of Canc&uacute;n. </P>

<B><P>HOW WILL these issues affect the internal politics of the developing countries?</P>
</B>
<P>ONE OF the major reasons for the success in Canc&uacute;n is the national mobilization in countries such as Brazil or in India, where the civil society is basically demanding that the governments represent them in negotiations instead of selling out the country.</P>
<P>But we can also think of Bolivia, where the whole struggle against the privatization of water has sparked not just a national debate, but also an international debate around the privatization of services. Those are the national mobilizations that, in terms of our organizing, we will be looking at--how do we assist those movements, and how do we make governments accountable to the people? </P>

<B><P>HOW SHOULD the global justice movement relate to the antiwar occupation movements?</P>
</B>
<P>I DON'T think that this a separate movement challenging the invasion and occupation of Iraq. I've just done a paper called "Open Fire, Open Markets," which explains U.S. foreign policy--the new Monroe Doctrine of America. That is, "we will control the world economically," with the military underpinnings left discreetly unsaid.</P>
<P>When some people say, "It's too simple to say that this was a war about oil," they're right. It was about oil, it was about ports, about schooling systems, about hospitals. It was about creating a dream economy, completely privatized and foreign-owned. The movement that has come together and that is in the streets challenging the occupation and invasion of Iraq is actually the same one as in the streets of Canc&uacute;n, or wherever these economic organizations meet.</P>

