<P><font face="Times New Roman, Times, serif" size="4">Used as cannon fodder in a war for oil and empire</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bring the troops home!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 10, 2003 | Page 1

<P><font face="Times New Roman, Times, serif" size="3">FRANK MENDEZ has had enough. And he wants the politicians and generals in Washington who issue his orders to know it.</P>
<P>Mendez is a U.S. Army reservist stationed in Iraq. While home on leave last week, he led a small demonstration in New Jersey--calling for U.S. soldiers to be brought home for good.</P>
<P>Mendez represents the growing doubts among rank-and-file soldiers carrying out Washington's occupation for oil and empire in Iraq. They were told that they would be welcomed as liberators.</P>
<P>But they now face the constant threat of armed attacks--while the top brass in Iraq lives in security and comfort, often in the very palaces once occupied by Saddam Hussein and his regime. To prove just how little Washington's warlords care about the soldiers that they sent to Iraq, the Pentagon has been extending tours and canceling leaves for thousands of troops.</P>
<P>Maria LaMonica's husband is part of a unit stationed in Kuwait that was told that they would have to buy their own plane tickets to get back to the U.S. for leave. After shelling out up to $2,500 a piece for the tickets, they found out that the leave had been canceled.</P>
<P>LaMonica says that her husband "wants out as soon as he gets home. He loves his country," she said, "but at this moment, he hates his government."</P>
<P>If conditions for rank-and-file U.S. soldiers are growing worse, they are nightmarish for Iraqis suffering the brunt of the occupation. Seven months after the fall of Saddam Hussein's government and Washington's promise that the country would be rebuilt as a model of democracy and prosperity, Iraq remains a shambles.</P>
<P>According to British journalist Robert Fisk, as many as 1,000 Iraqis die every week from causes related to the occupation--shootings by U.S. troops, lack of food and medicine, and above all, the wave of violence and crime that followed the U.S. takeover. As for "democracy," Iraq's U.S.-appointed governing council is so irrelevant that its members last week protested to their American overseers that they were seen as puppets.</P>
<P>This is the reality of the U.S. war on the Iraqi people. The lies to justify it have unraveled, one after another--and now more and more people are rejecting the price that the Bush administration wants us to pay.</P>
<P>We have a chance to give a voice to this growing opposition--at antiwar demonstrations on October 25 in Washington, D.C., and San Francisco. We can speak out against Bush's brutal occupation and show our support for the U.S. soldiers who want to come home--now. Get ready to get to Washington and San Francisco on October 25--and raise your voice against war and occupation.</P>



