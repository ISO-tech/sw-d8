<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Is George Bush a fascist?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 10, 2003 | Page 9</P>

<P><font face="Times New Roman, Times, serif" size="3">THE WORD "fascism" is used broadly on the left as a term of abuse. Sometimes it is used to refer to any repressive government, whatever its political form. Most commonly on the left in the U.S., it is used to describe any <I>Republican </I>government--in particular, any Republican government or candidate on the eve of a presidential election.</P>
<P>But fascism has a far more precise definition. Historically, fascism is a far-right <I>movement</I> of the middle classes (shopkeepers, professionals, civil servants) who are economically ruined by severe economic crisis and driven to "frenzy." </P>
<P>In the brilliant words of Leon Trotsky, fascism brings "to their feet those classes that are immediately above the working class and that are ever in dread of being forced down into its ranks; it organizes and militarizes them...and it directs them to the extirpation of proletarian organizations, from the most revolutionary to the most conservative."</P>
<P>Fascism unites the middle classes on the basis of the "nation" and race, under the leadership of some iron-fisted leader who will solve the crisis and restore "national greatness." But while fascism appeals to the middle class on the basis of a kind of "fool's socialism"--anti-Semitic criticism of the role of big business, for example--fascist movements do not bring the middle class to power.</P>
<P>As Leon Trotsky wrote: "German fascism... raised itself to power on the backs of the petty bourgeoisie, which it turned into a battering ram against the organizations of the working class and the institutions of democracy. But fascism in power is least of all the rule of the petty bourgeoisie. On the contrary, it is the most ruthless dictatorship of monopoly capital."</P>
<P>In power, as we know, fascism ruthlessly crushed even the most limited forms of parliamentary democracy. Clearly, this is not the character of the conservative state under which we current live. Moreover, if we were to accept the wrong definition of fascism (repression), then we would be forced into the position of saying that the <I>Democrats </I>are also "fascist."</P>
<P>Even the "freest" electoral system in the world keeps in reserve special laws designed to nullify various democratic rights in the name of "national security" or "emergency." In the U.S. historically there has been a great deal of legal and also violent repression against working-class struggle and other social movements, regardless of the party in power.</P>
<P>Democratic president Woodrow Wilson pushed through the Espionage Act during the First World War that sent more than 1,000 people to jail for speaking out against the war. After the war, Wilson rounded up and deported 6,000 foreign-born radicals.</P>
<P>Franklin Delano Roosevelt, considered the archetypal Democrat, forced 120,000 people of Japanese descent into concentration camps. Under his presidency, troops were used 43 time to quell labor disputes.</P>
<P>In 1948, Democrat Harry Truman--the man who began the Cold War witch-hunts--ordered the army in to seize control of the railroads to stop a railroad workers' strike. In the 1960s, the FBI's secret COINTELPRO operation against activists was begun and thrived under Democratic administrations. Clinton's Anti-Terrorism and Effective Death Penalty Act paved the way for the even more repressive laws being pushed these days. </P>
<P>If we lived in a fascist state, it would be impossible to even publish this newspaper, let alone print this article. To cry "fascism" every time a Republican is in the White House is to drastically underestimate what fascism really is. Secondly, it feeds illusions in the idea that the Democrats are somehow <I>less</I> likely to resort to police measures to attack working-class and political movements.</P>
<P>You can't have it both ways. Either fascism is a police dictatorship resting on the middle class and backed by big business (the meaningful definition), or it is simply a word for repression. Calling the Bush administration "fascist," without also calling equally repressive Democratic administrations  "fascist" is simply a way of scaring progressives into voting the lesser evil.</P>


