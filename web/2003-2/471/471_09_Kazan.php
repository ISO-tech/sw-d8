<P><font face="Times New Roman, Times, serif" size="5"><b>How Elia Kazan should be remembered</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 10, 2003 | Page 9</P>

<P><font face="Times New Roman, Times, serif" size="3">DIRECTOR ELIA Kazan died in September at the age of 94. He was eulogized for his illustrious career as director of famous plays and films like <I>On the Waterfront</I> and <I>East of Eden. </I>But for the thousands of people blacklisted during the McCarthyite witch-hunt of the 1950s, Kazan's legacy is anything but illustrious.</P>
<P>During the witch-hunt, thousands of Hollywood screenwriters, directors and actors were called up to testify before the House Un-American Activities Committee. Hollywood was an important target for the anti-communist witch-hunters. The message was, if they could put away a communist in Hollywood, they could go after anyone.</P>
<P>Big-name stars and little-known industry people alike were called upon to name names of coworkers and friends who they suspected were communists. A number of individuals called before the committee refused to give into the witch-hunters' demands. Some served time in jail time as a result; many more were barred from working in Hollywood again.</P>
<P>Kazan took another, less dignified route. Kazan, who was briefly a Communist Party member in the 1930s, went before the committee to finger eight Hollywood "communists."</P>
<P>While many of the people he named had already been named by others, this wasn't the point. At the time, Kazan was already a celebrated director. Instead of standing up to the fanatical witch-hunters, he lent them credibility.</P>
<P>The result is that while Kazan's name is still famous, the names of hundreds of talented directors disappeared without a trace. Throughout his life, Kazan remained not only unrepentant--but defiant about his back-stabbing. His 1954 film <I>On the Waterfront </I>views like an ode to ratting out your friends. Years later, Kazan maintained, "I'd had every good reason to believe the Party should be driven out of its many hiding places and into the light of scrutiny."</P>
<P>As the late blacklisted screenwriter Abraham Polonsky said in 1999, "I wouldn't be wrecked on a desert island with him, because if he were hungry, he'd eat me alive." That's how Kazan should be remembered.</P>



