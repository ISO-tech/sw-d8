<p><a name="Grocery"></a><font face="Times New Roman, Times, serif" size="4"><b>Southern California grocery workers</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Evan Kornfeld</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">LOS ANGELES--Union members were preparing to vote on whether to strike at three Southern California grocery chains, as <I>Socialist Worker </I>went to press. Three weeks of negotiations between the United Food and Commercial Workers (UFCW), which represents the 71,000 workers, and the Vons, Ralph's and Albertson's supermarket chains failed to reach an agreement as the contract expired on October 5.</P>
<P>Management is demanding big increases in deductibles and co-pays for doctor and hospital visits and for prescription drugs, a reduction in employer contributions to employee pension plans, and a two-tier system for employees. Union members were preparing for a strike authorization vote October 8-10 and could be walking the picket line by October 12.</P>
<P>All of Southern California's labor councils are holding rallies October 9 to show their solidarity. UFCW spokesperson Barbara Maynard told <I>Socialist Worker</I> that several unions, including the Teamsters and SEIU, have pledged not to cross picket lines.</P>
<P>UFCW Local 770 President Rick Icaza says, "70,000 unionized grocery workers are ready to stand together from San Luis Obispo to San Diego, from Santa Monica to the Arizona border. Together, we are ready to say 'No Way!'"</P>

