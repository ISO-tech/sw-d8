<p><a name="Miami"></a><font face="Times New Roman, Times, serif" size="4"><b>Miami University</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Molly Seifert</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">OXFORD, Ohio--About 400 janitors, groundskeepers, maintenance and dining hall workers at Miami University (MU) walked off the job September 26 to demand a living wage. MU is considered Ohio's "Ivy League" public university, with the highest tuition of any state school and an average annual tuition increase of 10 percent.</P>
<P>Yet starting wages at prestigious MU are about 20 percent lower than the vast majority of colleges in Ohio. For example, a custodian at Ohio University in Columbus has a starting wage of $10.16 an hour while MU custodians start at just $8.14 an hour.  </P>
<P>"They are paying us peanuts, and a lot of us are having to go on welfare and rely on food stamps," said Randy Marcum, a maintenance technician. Wages at MU are so low that approximately 25 percent of employees are on some form of government assistance--food stamps, housing, medical or school-lunch programs. Krista Willis, a housing and dining worker, added, "I don't make enough to pay the babysitter."  </P>
<P>While negotiations have remained at a standstill, many MU students and faculty are showing solidarity with the AFSCME workers. About 300 people marched last Friday, and students and faculty have erected a "tent city" to put pressure on the administration.</P>

