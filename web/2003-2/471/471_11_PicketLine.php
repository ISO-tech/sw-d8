<P><p><font face="Times New Roman, Times, serif" size="4"><b>Tyson Foods</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Marshall Braun and Nathan Fuller</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">JEFFERSON, Wis.--Going into the eighth month of their strike, Tyson workers are taking their fight to other Tyson meatpacking plants around the country. In late September, at least five "Truth Squads" from Jefferson traveled the Midwest in an effort to spread their struggle to non-unionized Tyson plants.</P>
<P>In one afternoon, more than 300 workers at a Tyson plant employing 1,200 in Storm Lake, Iowa, signed union cards as management nervously looked on. Jefferson strikers also stopped at Wal-Mart stores along the route to pass out leaflets outlining Tyson's deplorable labor practices.</P>
<P>Meanwhile, support for the strikers in Jefferson remains solid. An area dentist is giving free care to strikers, and police are arresting scabs for invalid vehicle registrations. At a September 28 rally, more than 300 supporters listened as heads of various local unions, a Mexican immigrant worker organization and student groups expressed solidarity with the ongoing struggle against Tyson's greed.  </P>
<P>Reaching out to other Tyson workers is a step forward, but it would make more sense to organize for solidarity action in plants that are already organized by the UFCW--focusing on the pepperoni plants that are filling the Jefferson plant's orders.</P>

