<p><a name="UCDavis"></a><font face="Times New Roman, Times, serif" size="4"><b>University of California</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Sarah Barnes and Chad Walsh</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">DAVIS, Calif.--Graduate student teaching assistants, tutors and readers who make up Local 2865 of the UAW struck October 3 at eight campuses of the University of California (UC). At stake among the 64 Unfair Labor Practice charges that the union has filed against the university is the union's right to honor other unions' picket lines.</P>
<P>"It is unacceptable to single out the UAW and treat us differently," said Rajan Mehta, a bargaining team member and head steward at UC Berkeley, in a union press release. "Moreover, if the UC administration truly wants to stop sympathy strikes, it should stop its unlawful and uncooperative labor relations practices that give rise to primary strikes."</P>
<P>On the Davis campus, about 60 teaching assistants and tutors picketed and marched all day as many of the classes that they normally teach were either canceled or had to be taught by the faculty.</P>
<P>Lots of undergraduate students came out as well to show their support. One sociology professor even assigned her entire class to interview people on the picket line. Members from other campus unions that represent lecturers as well as the service, clerical and professional-technical employees also showed up to express their solidarity.</P>



