<font face="Times New Roman, Times, serif" size="4">From the disaster in Iraq to the scandal over the "Wilsongate"...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Has Bush blown it?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 10, 2003 | Page 3

<P><font face="Times New Roman, Times, serif" size="3">THE BRIDGE is burning at both ends, and the Bush administration is stranded in the middle. The idea that George W. Bush would waltz to a reelection landslide as a war leader has been shattered by the armed resistance in Iraq and a series of scandals over the intelligence used to justify the war.</P>
<P>There's no easy way for Bush to get back into the White House via domestic issues, either. A steady stream of terrible economic news--most recently, the fact that some 2.4 million people lost their health insurance last year--has highlighted the fact that the "recovery" has benefited only a tiny few. Even the increase in jobs announced last week--a net gain of 58,000 for September--is tiny compared to the nearly 3 million jobs lost under Bush's watch.</P>
<P>All this has finally burst the bubble of Bush's popularity.According to a CBS News/<I>New York Times </I>poll, "Just 45 percent of Americans now have confidence in Mr. Bush's ability to deal wisely with an international crisis, down sharply from 66 percent in April." </P>
<P>For Bush, the failure to find weapons of mass destruction in Iraq was bad enough. But the big scandal right now is over the White House's exposure of Victoria Plame as an undercover CIA agent--as revenge against Plame's husband, former diplomat Joseph Wilson, who criticized the claims that Iraq sought African uranium for its mythical nuclear weapons program.</P>
<P>In leaking Plame's name, administration officials wanted to intimidate their opponents, but their arrogance is coming back to haunt them now.  "Wilsongate" has become a way for Bush's rivals to bash the administration--not only Democratic presidential contenders, but even the CIA bureaucracy furious at the White House for pushing them aside for its own political agenda.</P>
<P>The split at the top is welcome--and provides new opportunities for the antiwar movement to make its voice heard in the mainstream political debate. As that movement reemerges, there is a debate over what its demands should be.</P>
<P>On the one hand, the group Military Families Speak Out will be sending a contingent of soldiers' relatives to march in Washington, D.C., on October 25--under the slogan "bring the troops home now." Others in the antiwar movement look to a different solution--such as the group Win Without War, which calls for "ending the U.S. military occupation of Iraq by immediately transferring full authority to the United Nations for the transition of the country to a truly representative government."</P>
<P>But even with a UN operation in place, the Pentagon would still call the shots in Iraq--just as it does in the UN administration in Bosnia. The call to "UN-ize" the occupation of Iraq has been taken up by leading Democratic presidential candidates like Wesley Clark, the retired general who, as head of NATO, ran the brutal 1999 war over Kosovo.</P>
<P>"In the court of international opinion, the UN's authority carries substantial weight," Clark wrote in the <I>New York Review of Books</I>. "All of this was potentially available to the United States--if only our government had seen that it was necessary and pursued it." For Clark, as well as Democratic rival Howard Dean, the problem wasn't waging war on Iraq--but the Bush administration's go-it-alone strategy.</P>
<P>The Democrats are willing to use the disaster in Iraq to score political points against Bush, but they're unwilling to end Washington's rule over that country. Their aim is to give the occupation a new label and better public relations--and cut the losses before there's a full-blown crisis in U.S. imperialism.</P>
<P>There was never any justification for the invasion--and there is no justification for an occupation. That's why we need to mobilize for the biggest possible demonstration on October 25 to speak out against the occupation of Iraq.</P>


