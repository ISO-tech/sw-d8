<P><font face="Times New Roman, Times, serif" size="5"><b>What's next after the California recall?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Todd Chretien</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 10, 2003 | Page 2</P>

<P><font face="Times New Roman, Times, serif" size="3">THE CALIFORNIA recall election was coming down to the wire as <I>Socialist Worker</I> went to press. With hours to go before the polls opened, even the media's election "experts" were unsure whether Gov. Gray Davis would be voted out of office--and if he was, which candidate would replace him. But whichever of the three most likely outcomes wins out, workers and the poor in California will lose.</P>
<P>A week before the vote, Republican action figure Arnold Schwarzenegger looked like a shoo-in to be the next governor. His support in opinion polls was growing along with backing for Davis' recall--mainly because Ah-nuld's handlers kept him away from reporters and reduced his public statements to stupid sound bites like "I will clean house."</P>
<P>But last week, more than a dozen women in all came forward in the media to charge Schwarzenegger with sexual harassment and possible sexual assault. His public apology was rightly taken by millions of Californians as a confession to the numerous allegations--and his election support began dropping.</P>
<P>Next came the transcript of a 1975 interview, in which the Terminator said: "I admire Hitler because he was a good public speaker...and what he was able to do with it." Incredibly, Schwarzenegger had a chance of surviving the uproar and coming out on top in the election.</P>
<P>But it is just as incredible that this Hitler-loving sexist pig was taken seriously as a candidate in the first place. That shows the deep hatred for Davis.</P>
<P>In his five years as governor, he gave away billions to his friends in the energy industry and presided over a massive budget deficit--all the while slashing away at spending for schools, hospitals and social spending. Hundreds of thousands have lost their jobs, a generation of young Black men were warehoused in the state's ever-growing prison system, and poverty shot up.</P>
<P>Davis was desperately playing the "lesser-evil" card as the election approached--his only hope for holding on to power. Meanwhile, Lt. Gov. Cruz Bustamante, the main Democrat competing with Schwarzenegger to replace Davis, was in trouble because he bears the same responsibility for California's crisis as Davis.</P>
<P>In the days leading up to the vote, Bustamante was picking up support as Ah-nuld fell in the polls. But even if this "lesser of three evils" manages to squeak into the state house, his administration will undoubtedly continue the Davis agenda.</P>
<P>Media pundit Arianna Huffington had been the hope of some progressives in California for her supposedly "independent campaign. But disgracefully, she dropped out of the race with a week to go--and jumped on the Davis gravy train.  </P>
<P>Green Party candidate Peter Camejo, on the other hand, fought his way through all televised debates and presented a real alternative to millions of Californians sick of the status quo. His plan to raise taxes on the wealthiest 5 percent--and pour the money into health care and education--struck a chord. Defying conventional wisdom, Camejo spoke out strongly against the U.S. occupation of Iraq, the death penalty and policies that discriminate against undocumented workers.</P>
<P>The mainstream media patronizingly dismissed Camejo's left-wing positions as "unrealistic." But the day after the first major debate, he won an Internet poll in the <I>San Francisco Chronicle </I>asking which cnadidate did the best.</P>
<P>Days before the election, Camejo spoke out in the last televised debate, giving voice to what millions of Californians are thinking. "We've now heard that [Schwarzenegger] molests women--that he's a predator," Camejo said. "And I'll tell you something: if he was a Black man, he'd be in jail; if he were brown, he'd be in jail; if he were a poor white, he'd be in jail! What does it tell us about our society that a rich white person can do the type of things that he's alleged to have done?"</P>
<P>Although he won't be the next governor, Camejo showed how to stand up against the Republicans without submitting to the Democrats lesser-evil blackmail. That spirit will have to be organized into action in every school, hospital, neighborhood and workplace if we are going to stop the budget assault--which will only pick up steam in the coming months, no matter who comes out on top on October 7.</P>



