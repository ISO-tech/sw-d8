<P><font face="Times New Roman, Times, serif" size="4">Rutgers activist targeted</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Palestinian conference under fire</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 10, 2003 | Page 12</P>

<P><font face="Times New Roman, Times, serif" size="3">ACTIVISTS AT Rutgers University in New Jersey are under fire. Their "crime"? Organizing a campaign to express solidarity with the struggle of Palestinians.</P>
<P>This coming weekend, New Jersey Solidarity-Activists for the Liberation of Palestine will host a North American Student Conference of the Palestine Solidarity Movement at Rutgers--expected to be one of the largest gatherings of pro-Palestinian activists in the U.S. this year. But under heavy pressure from pro-Israel groups, Rutgers has been trying for months to force the conference off campus.</P>
<P>Last month, administrators tried to cancel the meeting--claiming a problem with "logistics." But the truth is that the university objects to the politics of the conference. </P>
<P>That's why it decided to host "Israel Inspires"--a pro-Israel conference, also scheduled for October 10-12, with its stated aim to "neutralize the Palestinian movement." Featured participants include Pentagon hack Richard Perle, former CIA Director James Woolsey, and Tom Rose, publisher of the right-wing <I>Jerusalem Post</I>. </P>
<P>Meanwhile, Charlotte Kates, one of the main organizers of the Palestinian solidarity conference, has become a target for pro-Israel extremists. Flyers were distributed on campus that include Kates' picture--along with her address and phone number, and her parents' address--and invite people to call and "pay her a visit."</P>
<P>Another flyer pretending to be a poster for the pro-Palestinian conference has a photo in which Ku Klux Klan members have been retouched to look like Arabs--equating Nazis with those working to end Israel's apartheid. "[Kates has] been harassed--herself and a few others--for quite some time," said Elias Rashmawi, a coordinator of the Free Palestine Alliance and speaker at the conference. "This is typical whenever you're working on the Palestine issue."</P>
<P>Kates and the other activists at Rutgers have vowed to go ahead with their conference. "We refuse to be silenced," she said in a statement. "We will hold our conference wherever we must--in a hotel, in a park, wherever. The Palestinian people have continued to resist, despite incredible and overwhelming force displayed against them--and we owe them nothing less than to refuse to be silenced." </P>

