<P><font face="Times New Roman, Times, serif" size="5"><b>The vile record of a racist "Indian fighter"</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3"><B>Dear <I>Socialist Worker,</B></I><br>
Rep. William Janklow (R-S.D.) killed a man after running a stop sign at high speeds in South Dakota last month. He is now being prosecuted for manslaughter, and if convicted, he could face 10 years in prison.</P>
<P>While the media has focused on Janklow's horrendous driving record, it has ignored his record as one of the worst racists in modern U.S. history. Janklow emerged as a political figure in South Dakota in the mid-1970s, as a self-declared "Indian fighter" leading a racist backlash campaign against the Native American rights activists of the American Indian Movement (AIM). </P>
<P>South Dakota is the home of two of the largest Native American reservations in the country, Pine Ridge and Rosebud. It became a battleground between AIM activists fighting for greater dignity and human rights--and the FBI, the state of South Dakota and corrupt tribal governments committed to repressing them.</P>
<P>Janklow, a former tribal council lawyer, took the lead in trying to crush AIM. While running for South Dakota attorney general in 1974, he declared: "The only way to deal with the Indian problem in South Dakota is to put a gun to AIM leaders' heads and pull the trigger." </P>
<P>In 1976, he tried to frame AIM activist Dennis Banks on trumped-up charges. Banks fled to California, and then-California Gov. Jerry Brown announced that he would refuse to sign any extradition order because of Janklow's death threats against AIM leaders.</P>
<P>During the 1970s, dozens of Native Americans were killed by various agencies of the federal and state government and vigilante squads. Others were sent to prison on phony charges, the most famous being Leonard Peltier.</P>
<P>Janklow parlayed the deeply ingrained racism against Native Americans in South Dakota into being elected governor in 1978--and more recently, the state's only representative in the U.S. House. Now that Janklow is back in the spotlight, greater justice could be served for his many victims by opening his whole political life to examination--not just his driving record.</P>

<B><P>Joe Allen, </B>Chicago</P>



