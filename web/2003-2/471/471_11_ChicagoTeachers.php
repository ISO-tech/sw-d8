<P><font face="Times New Roman, Times, serif" size="5"><b>Delegates reject teachers' deal in Chicago</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jesse Sharkey, CTU Delegate</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 10, 2003 | Page 11</P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--Delegates of the Chicago Teachers Union (CTU) voted October 1 to reject the negotiating committee's tentative contract proposal by a vote of 402 to 289. The union's House of Delegates meeting was overflowing with union members to hear CTU president Deborah Lynch present the details of a five-year proposal.</P>
<P>But Lynch didn't get far into the details of the offer--a 4 percent annual raise, offset by increased health care costs, a longer work day, and no meaningful improvement in class size or guaranteed preps for elementary teachers--before angry teachers began to interrupt and heckle her. Lynch appealed for calm, insisting that the negotiating team had done their best and that they should be allowed a chance to make their case.</P>
<P>But she lost control of the meeting during the question period, as teachers poured out years of frustration over grueling conditions, lousy pay and a lack of respect. One teacher recounted what it was like working with no break, arriving at 7:30 a.m. and eating her lunch at 2:30 p.m.</P>
<P>Others spoke of overcrowded classrooms, working hundreds of hours to coach elementary sports for $400 per year--or their fear of dying before they could collect their pension. One teacher explained that the cost of his medications would jump from $400 per year to $2,500.  </P>
<P>As a result of the delegates' vote, the union's entire membership will now vote on the proposal October 16. CTU leaders are describing the delegates' "no" vote as an act of sabotage by the old guard, and claim that a "no" vote on the 16th will lead to a strike. But the old guard only control about 100 delegates' votes.</P>
<P>The deal was rejected because Lynch's election, as well as her rhetoric, raised expectations that the union would fight for a better deal. The union leadership did virtually nothing to involve the union membership, nor did Lynch speak out or advocate on our behalf in the press. The union's mobilization committee hasn't met since May.</P>
<P>In reality, a "no" vote would force the union to continue to fight--and no strike could happen without the membership's vote. The mainstream media has joined the campaign to convince teachers to settle, with an editorial titled, "Earth to Teachers" in the <I>Chicago Tribune.</P>
</I><P>But don't expect the Tribune Company to give up its share of the $200 million per year tax break Illinois gives to newspapers--whose ink and newsprint are tax-free--to help fund the schools. Now delegates must convince union members that we can fight for more--and to vote "no" on October 16.</P>



