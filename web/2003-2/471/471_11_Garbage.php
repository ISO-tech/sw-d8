<P><font face="Times New Roman, Times, serif" size="4">3,300 garbage workers on strike in Chicago</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"We want what's fair"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lauren Fleer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 10, 2003 | Page 11 </P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--Some 3,300 sanitation workers, members of Teamsters Locals 731 and 301, declared a strike after their contract expired at midnight September 30. After contract negotiations broke down, the Chicago Refuse Haulers Association (CRHA)--the bargaining agent for the Chicago area's 16 largest, private trash disposal companies--also promptly declared a lockout.</P>
<P>The garbage workers, now earning between $10 and $21 per hour, are seeking a three-year contract including hourly wage increases of $2 in the first year, $2.15 in the second, and $2.40 in the third. Management's last offer Tuesday included only an 80 cents per hour increase, which would dwindle down to a pitiful 70-cent increase over the course of their proposed five-year deal. </P>
<P>"Those increases do not even cover the maintenance costs of health insurance benefits," Teamsters spokesman Brian Rainville told reporters. "So basically, their proposal amounts to a wage freeze and a cut in health benefits." Jim, an employee of Waste Management, Inc., told <I>Socialist Worker</I>, "I mean, we're not asking for anything ridiculous. We just want what's fair. And I see nothing wrong with that. Don't try to take something away that we've worked hard for all these years. I've been with Waste Management for 14 years."</P>
<P>Millions of residents and business owners were affected by the work stoppage, including commercial and industrial customers and apartment buildings with more than four units. Smaller apartment buildings and single-family homes in Chicago are serviced by city garbage workers, who are represented by another Teamsters local and not involved in the strike.</P>
<P>Unfortunately, Local 731 officials invited those city workers to "pick up the slack"--scab on members of their own union! Local 731  Secretary-Treasurer John Doerrer sent a letter to a top aide of Chicago Mayor Richard Daley authorizing city workers to take over the trash collection in and around Wrigley Field, to prevent the "city's image from being marred by uncollected refuse" during the Chicago Cubs October 3playoff game.</P>
<P>Despite this self-defeating gesture, the union has shown no other sign of backing down. Members rejected the CRHA's "last, best, and final offer" October 5 by a 92 percent margin, because it still fell 50 cents per hour short of their demand. </P>
<P>The Teamsters announced that they are willing to negotiate further, but CRHA spokesman Bill Plunkett said that they are planning for no new negotiations-- and that it's up to the Teamsters to resolve the strike. If there's a public health risk from uncollected garbage, as city officials claim, management  must bear the responsibility.</P>
<P>These workers deserve the decent contract they set out for--and other Teamsters shouldn't be scabbing on this strike, no matter what union officials leaders and the mayor says. As Willie, of Local 731, remarked, "If we could get something like a fair and a decent raise, we'd all be happy out here. Each and every one of these guys wants to be on the truck today. The longer the wait, the bigger the pile-up we're going to have out there. We've all got mortgages, we've all got families, you know, it's scary, but we gotta do what we gotta do."</P>
<I><P>Carole Ramsden and Kirstin Roberts contributed to this report.</P></I></FONT></BODY>



