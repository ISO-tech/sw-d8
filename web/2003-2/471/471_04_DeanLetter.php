<P><font face="Times New Roman, Times, serif" size="4">Democratic frontrunner shows his real colors</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Dean: Myth and reality</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 10, 2003 | Page 4

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I><br>
A lot of ink has been spilled in the mainstream media about the "electrifying" grassroots campaign of so-called "maverick" and "straight-talker" Howard Dean. The moderate former governor of Vermont has tapped into the growing anger against Bush and has used it to position himself as a "radical"--a label he is quickly moving to shed now that he's become a frontrunner for the Democratic nomination.</P>
<P>About 10,000 people crowded into New York City's Bryant Park in late August to witness the culmination of Dean's "Sleepless Summer" tour. The clear message of the festivities and Dean's speech was that, now that the former Vermont governor is the man to beat, it's time to "clarify" his image. </P>
<P>In fact, the basis for his reputation as a "leftist" extends no further than his willingness to bash Bush. The real message of the "Take Back America" rally was that Dean has appeal among all types of voters--even Republicans.</P>
<P>The rally lost any pretense of being a left event early. The two most liberal voices of the program--Democratic congressmen Jerrold Nadler and Major Owens--went first and made claims that went far beyond Dean's actual positions. Owens proclaimed that Dean would end the occupation of Iraq. "We're spending $1 billion a week, and Deans knows this," Owens said. "He has vision...He is the champion of working families...and he will stop the war!"</P>
<P>But Dean's fabled antiwar stance proved to be just that: a fable. As he made clear at the rally, "I supported the first Gulf War. I supported the Afghan War. They murdered 3,000 of our people and I felt it was a matter of national security."</P>
<P>And rather than promise to bring the troops home, Dean pledged, "I would never send our sons and daughters to die overseas...without telling the truth about why we were going!" Some "dove."</P>
<P>More intriguing was the crowd's reactions to such pronouncements. Dean's clearest antiwar rhetoric received loud applause, while his more tempered remarks were met mostly with polite clapping or silence. I think this underscores the trend of where Dean's campaign will go. He's rounding up the left now, only to pull them to the middle with him as Election Day gets closer.</P>
<P>Other speakers at the rally further honed Dean's moderate image. Alex Munoz, a Columbia University graduate and Army recruit stationed at Fort Benning said to the audience: "Some people say that Howard Dean is too liberal to be elected president. I'm here to tell you that's wrong. I am a moderate Republican. I am a Special Forces recruit. And I support Howard Dean!"</P>
<P>After that, it was Dean's turn. After firing the crowed up with some barbs tossed Bush's way, Dean got "practical." That meant saying that balancing the budget would be a prime goal of his administration. Details of how that was to be accomplished, however, were sorely lacking. We do know that it won't come from cuts in defense.</P>
<P>Dean said that Bush "doesn't understand defense"--and said he has no intention of deflating the bloated defense budget. Even more alarming was that the rally was little more than a fundraising event that only served to illustrate the primacy of money--not grassroots activism--in Dean's campaign.</P>
<P>In reality, leftists trying to influence the Dean campaign are more likely to be pulled into the Democratic Party than they will be able to pull the party to the left. For socialists, the Dean campaign illustrates perfectly how campaigning for a "lesser evil" only brings about the "lesser" and the "evil."</P>
<P>Not everyone was buying Dean's reputation. Jews Against The Occupation displayed a "Free Palestine" banner at the event and handed out literature criticizing Dean's support for the most right-wing pro-Israel positions. In an interview with <I>Forward</I> newspaper last fall, Dean said that his view of the conflict "are much closer to AIPAC"--the pro-Sharon American-Israel Public Affairs Committee--than to Peace Now, a more liberal formation.</P>
<P>In the end, though, there was one thing Dean did get right in his speech. "The truth is, the power to change the country is in your hands, not mine," he said. That's right. Our only option in the next 13 months is to keep organizing in the streets against Bush. That's truly our only "realistic" strategy.</P>

<B><P>Peter DiLeo, </B>New York City</P>


