<P><font face="Times New Roman, Times, serif" size="5"><b>Bush overtime scam beaten</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 10, 2003 | Page 2</P>

<P><font face="Times New Roman, Times, serif" size="3">IN A victory against the Bush administration's war on workers' rights, the House of Representatives voted 221-203 October 2 to block a rule change that would drastically restrict workers' overtime rights. Backed by big business interests, the administration's proposed rules would redefine which workers are eligible for overtime--time-and-a-half after workers have worked more than 40 hours a week.</P>
<P>The right to overtime pay is part of the Fair Labor Standards Act, which was won in the 1930s to keep workers from working long hours of overtime for little money. Under the administration's proposed rules, the cap on what workers can make and still receive overtime pay will rise from the outdated figure of $8,060 a year to $22,100.</P>
<P>But the new rules would cut off millions of workers who "make too much"--about $425 a week--who can be defined as "management" by their employers, or who have some additional job training or education beyond high school. Among the occupations that could lose overtime rights are assistant managers at retail stores, emergency medical technicians or cooks. Union officials say that as many as 8 million workers now eligible for overtime could lose this right if Bush's Labor Department gets its way.</P>
<P>The House vote is the latest in a series of defeats for the administration on the overtime issue. Twenty-one House Republicans broke ranks to join Democrats in voting for legislation that would bar the Labor Department from putting the rules into effect.</P>
<P>Just two months ago, eight of those same Republicans voted to support the overtime rules, in a 213 to 210 vote in favor of the administration's position. Last month, the Senate voted to stop the rules changes as well.</P>
<P>One defector, Rep. Todd Tiahrt (R-Kan.), said he changed his mind after talking to aerospace workers in his district. "I'm just looking at the future and what it's going to take for these people to have an ability to earn overtime wages," he told the <I>Los Angeles Times.</P>
</I><P>With Bush's approval ratings--about his policies at home and in Iraq--dropping like a rock, lawmakers are nervous about supporting another attack on workers. "It says the president is wounded," Stephen Hess, a senior fellow at the Brookings Institution, told the <I>Washington Post.</I> "This is the sort of thing that doesn't happen when the president's approval ratings are at 70 [percent] rather than at 50."</P>



