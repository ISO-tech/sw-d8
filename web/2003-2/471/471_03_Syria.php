<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Israel risks war with attack on Syria</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 10, 2003 | Page 3

<P><font face="Times New Roman, Times, serif" size="3">COPYING THE Bush administration's rhetoric, the Israeli government last weekend launched a missile attack on Syria--and then proclaimed that it was acting in "self-defense." The assault--the first Israeli strike deep into Syrian territory in 30 years--edged the entire Middle East toward a wider war.</P>
<P>The target was an alleged "terrorist training camp" outside of Damascus, supposedly run by Islamic Jihad, the Palestinian group that claimed responsibility for a suicide bombing in Israel last week, which killed 19 people and wounded dozens more. Like the U.S. claim that it was acting "pre-emptively" in its war on Iraq, Israel declared that the missile strike was an act of self-defense in the global "war on terror."</P>
<P>Never mind that the U.S. State Department's most recent report on international terrorism, released in April, doesn't mention such a facility. Or that a senior Bush administration official confided anonymously that the evidence offered by Israel to justify its attack was "very amorphous."</P>
<P>That didn't stop George W. Bush from condoning Israel's blatant violation of international law. "Israel must not feel constrained in defending the homeland," Bush said. For months now, the U.S. has been increasing pressure on Syria to implement a series of military and political changes--and pushing the so-called Syria Accountability Act, which would ban U.S. exports if the government fails to jump through the hoops quickly enough.</P>
<P>When Syria called for a United Nations (UN) resolution condemning the Israeli missile strike, the U.S. stood alone with Israel in opposing it. And in an ominous sign for the future, U.S. Ambassador to the UN John Negroponte told the Security Council that "the United States believes that Syria is on the wrong side of the war on terrorism."</P>
<P>The attack on Syria followed Israeli Prime Minister Ariel Sharon's statement that his government still intends to "remove"--that is, kill--Palestinian Authority President Yasser Arafat. Sharon's threats over the past several weeks have caused an international uproar--and led large numbers of Palestinians to camp outside Arafat's Ramallah compound to serve as "human shields" against an Israeli attack. Nevertheless, some in Sharon's cabinet are so intent on eliminating Arafat that they criticized the bombing of Syria as the soft alternative to following through on the threat on Arafat's life.</P>
<P>Meanwhile, the real source of the Israeli-Palestinian conflict is ignored. In the mainstream media, no one is willing to ask the key question: Why are Palestinian youth willing to volunteer for suicide missions? Like Hanadi Jaradat, the woman who blew herself up last week. Israel may claim that the suicide bombers have a training camp in Syria, but obviously, Jaradat never traveled there.</P>
<P>"Do Palestinian suicide bombers really need to practice suicide bombing?" wrote journalist Robert Fisk last week. "Does turning a switch need that much training? Surely the death of a brother or a cousin by the Israeli army is all the practice that is needed." Indeed, a brother and a cousin of Jaradat--a law school graduate who was an apprentice at a law firm--were killed by Israeli forces in June.</P>
<P>Jamal Dirawi will recognize Jaradat's despair. He was jolted awake by fists pounding on his front door after midnight. Born in the occupied West Bank just south of East Jerusalem--before it was occupied--he now can't get proper identification from Israeli authorities to stay legally.</P>
<P>He and his entire village are trapped, "illegally" inhabiting land that they never left. With Israel's monstrous separation fence on the fast track to completion, Dirawi doesn't know what to do. "A government man came [in March] and said they want this area as a no man's land, that they'll cut our electricity and water," Dirawi says. "Israel wants our land, but it doesn't want the people."</P>



