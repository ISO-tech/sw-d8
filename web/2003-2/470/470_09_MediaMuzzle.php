<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>How the media sold Bush's war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 3, 2003 | Page 9</P>

<P><font face="Times New Roman, Times, serif" size="3">CNN REPORTER Christiane Amanpour recently made news when she denounced the media for acting as cheerleaders for the Bush war drive against Iraq. "I think the press was muzzled, and I think the press self-muzzled," Amanpour said on CNBC's <I>Topic A with Tina Brown</I>. "Television was intimidated by the administration and its foot soldiers at Fox News." As if wanting to confirm Amanpour's charge, Fox issued the statement, "It's better to be viewed as a foot soldier for Bush than a spokeswoman for al-Qaeda."</P>
<P>Amanpour's charges are pretty mild when you consider that the press didn't just muzzle itself. In fact, it willingly and, often, consciously perpetrated fraud throughout the war. Amanpour's network spent millions of dollars to build two parallel news operations--one aimed at CNN's non-U.S. customers and one aimed at the U.S.</P>
<P>The CNN reports shown outside the U.S. took a more sober, evenhanded approach to the war. On the other hand, the CNN reports shown in the U.S. were relentlessly upbeat, filled with experts describing battlefield tactics as if they were reporting on the Super Bowl.</P>
<P>Anyone who watched war coverage on CNN--or MSNBC, CBS, Fox or ABC for that matter--had no doubt what team the announcers were rooting for. Yet Amanpour's self-criticism is rare to hear in the media these days. For the few who even recognize that the media may have something to account for, the attitude of <I>Newsweek</I> online columnist Christopher Dickey is probably more common.</P>
<P>With a straight face, Dickey claimed in a column last month "there were warnings in the American press about how long and painful the military occupation would be, if only the public had listened." Dickey's assertion about stories questioning the war is literally true.</P>
<P>But it's as true as the Bush administration's claims that it never said Saddam Hussein was behind the September 11 attacks. Because administration flaks never said the words "We have proof that Saddam Hussein was behind the September 11 attacks," we are supposed to believe that they never tried to link Saddam and 9/11. Millions of Americans think Saddam <I>was</I> behind 9/11 because the administration used every opportunity it could to imply, hint, suggest or intimate it.</P>
<P>No doubt a Lexis-Nexis search of media in the last year would turn up hundreds of stories questioning--or even opposing--the administration's plans to invade Iraq. Perhaps they wouldn't be found in the transcripts of cable TV scream fests. But they would turn up in print--in Saturday editions, in back sections, in small rewrites of wire stories and in radical publications.</P>
<P>In other words, they would be in the places where readers would be least likely to find them, while the front pages and headlines would be filled with pro-war propaganda. That's why it's so dishonest for Dickey to draw the conclusion he wants--that ordinary Americans are to blame for believing the lies the administration and the media peddled.</P>
<P>The vast majority of people don't have the time to read dozens of papers or to scan the Internet to investigate the latest administration scam. And yet, ordinary people have caught on to the administration's deceptions while much of the media continue to treat Bush and his cronies with kid gloves.</P>
<P>When Bush admitted last month that his administration had no evidence linking Saddam and 9/11, only the <I>Chicago Tribune</I> and the <I>Los Angeles Times</I> of major newspapers made the admission a front-page story. The <I>New York Times</I> and <I>Washington Post</I> downplayed it, and the <I>Wall Street Journal</I> didn't even mention it.</P>
<P>Still, Bush's popularity has plunged to the worst ratings of his administration. Ordinary people's reality is catching up with the White House and its media enablers. After all, it's their family members in uniform who are dying in Iraq. They're the ones who have lost those nearly 3 million jobs that have disappeared since Bush took office. No amount of media manipulation can hide these facts, and Bush is paying the price for thinking he could.</P>




