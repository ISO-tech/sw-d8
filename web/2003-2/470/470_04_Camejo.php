<P><font face="Times New Roman, Times, serif" size="5"><b>Camejo's campaign gives the right answers</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 3, 2003 | Page 4

<P><font face="Times New Roman, Times, serif" size="3"><B>Dear <I>Socialist Worker,</B></I><br>
Last week, millions of people saw Green Party candidate Peter Camejo take on The Terminator and the Lt. Budget Cutter in a televised debate for candidates in the California recall election. Camejo repeated again and again that California could solve its budget deficit and fully fund health care and education by taxing the rich.</P>
<P>While Democratic candidate Lt. Gov. Cruz Bustamante advocated offering some limited rights to undocumented immigrants, Camejo spoke out forcefully for full rights for immigrant workers and argued that people should be able to cross national borders to provide for their families.</P>
<P>Independent candidate Arianna Huffington relentlessly attacked the Terminator for his plans to slash health care and education. He responded by snorting out a thinly veiled threat of scripting her a part in <I>Terminator 4, </I>in which should would presumably die in a hail of futuristic gunfire.</P>
<P>Camejo condemned the "illegal occupation" of Iraq and pointed to the Green Party as a growing force that can challenge the bipartisan subservience to corporate America. The California antiwar movement and everyone who wants to stop the war at home against the working class and the poor should vote for Camejo on October 7.</P>
<P>Camejo's campaign is raising the right questions and giving the right answers:  end the occupation and tax the rich. Ralph Nader and the Green Party should follow Camejo's example and start preparing now to run an all-out, hard-hitting presidential campaign in 2004.</P>

<B><P>Todd Chretien</B>, Oakland, Calif.</P>

