<P><font face="Times New Roman, Times, serif" size="5"><b>The big lie</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 3, 2003 | Page 1</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><i>"The Iraqi regime...possesses and produces chemical and biological weapons. It is seeking nuclear weapons."</i><BR>
<B>George W. Bush</B>, October 7, 2002</P>
<P><i>"There's no doubt in my mind that they currently have chemical and biological weapons."</i><BR>
<B>Donald Rumsfeld</B>, January 7, 2003</P>
<P><i>"There is no doubt that Saddam Hussein now has weapons of mass destruction."</i><BR>
<B>Dick Cheney</B>, August 26, 2002</P>
<P><i>"We know that Saddam Hussein is determined to keep his weapons of mass destruction, is determined to make more."</i><BR>
<B>Colin Powell</B>, February 5, 2003</P>

<P>THE LIES are catching up with George W. Bush. A whole lot of them.</P>
<P>The Iraq Survey Group--a secret team of 1,400 scientists and intelligence experts led by David Kay, the former United Nations weapons inspector--is expected to issue its interim report soon. For more than four months, Kay--a pro-war hard-liner who cheered on the U.S. invasion as an analyst for MSNBC--has been hunting in Iraq for Saddam Hussein's supposed weapons arsenal.</P>
<P>"The bottom line," said a BBC News journalist, quoting an anonymous U.S. official, "is that the team has found no weapons of mass destruction." No wonder the White House staff is getting nervous. "I'll tell you what," a Bush operative told <I>Newsweek </I>magazine. "We really need to find some fuckin' WMD!"</P>
<P>Secretary of State Colin Powell won't be surprised by the Kay report. In a February 2001 speech that he gave in Cairo--a videotape of which was uncovered by antiwar journalist John Pilger--Powell touted the U.S. government's success at containing Saddam Hussein.</P>
<P>"He has not developed any significant capability with respect to weapons of mass destruction," he said then. "He is unable to project conventional power against his neighbors." That was before September 11--and the Bush administration's cynical exploitation of the hijackings as an excuse to wage war on the world. From then on, like the other war makers in Washington, Powell lied through his teeth about Saddam's non-existent arsenal to whip up support for an invasion.</P>
<P>The Bush gang's cynicism was on display again last week with press reports about how two top White House officials--as yet unknown--disclosed the identify of an undercover CIA agent to at least six Washington journalists. Why did Bush's henchmen commit this felony?</P>
<P>As retaliation against the agent's husband, former U.S. Ambassador Joseph Wilson, who publicly challenged the absurd U.S. and British claims that Iraq had tried to buy "yellowcake" uranium ore from Africa. Now the Justice Department has been pressured into opening up an investigation of the White House to find out who was responsible.</P>
<P>If all this wasn't bad enough for Bush, a letter from the heads of the House Intelligence Committee last week accused the CIA of using outdated intelligence on Iraq. And according to the <I>New York Times,</I> an internal Defense Intelligence Agency report found that Iraqi exiles recruited by Pentagon puppet Ahmed Chalabi "invented or exaggerated their credentials as people with direct knowledge of the Iraqi government and its suspected unconventional weapons program."</P>
<P>Then there's the biggest lie of all--that the U.S. war was about liberating the Iraqi people. Since the occupation began, U.S. forces have done next to nothing to aid the people of Iraq, who remain without such necessities as electricity and clean water.</P>
<P>But U.S. overseers have found the time to appoint a sham council to do their bidding. Millions of Iraqis are paying the price--in misery, violence and death--for Washington's occupation for oil and empire.</P>
<P>We can't let them get away with this lie one minute longer. That's why we say: U.S. troops out now!</P>



