<P><font face="Times New Roman, Times, serif" size="5"><b>Syria spy hysteria at Guant&aacute;namo</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 3, 2003 | Page 2</P>

<P><font face="Times New Roman, Times, serif" size="3">THE PENTAGON has arrested a U.S. Army chaplain and an Air Force translator and accused them of spying for Syria. But their only crime may well be treating the detainees at the U.S. military prison camp at Guant&aacute;namo Bay, Cuba, like human beings.</P>
<P>Capt. Yousef Yee, who was arrested on suspicion of espionage, has served as a spiritual advisor and interpreter at Camp Delta, which houses more than 660 people, most of them taken prisoner during the U.S. war on Afghanistan. Air Force Senior Airman Ahmad I Al-Halabi--who faces more than 30 charges, including aiding the enemy and committing espionage--also worked as a translator at the camp.</P>
<P>The Pentagon is trying to whip up hysteria over the fact that Al-Halabi is originally from Syria, and that Yee received religious instruction in the country--which the Bush administration has repeatedly accused of aiding and harboring terrorists. As Syrian Information Minister Ahmad Al-Hassan asked Agence France Press, "How could Syria have spies in Guantanamo? Is the CIA incapable of finding a trustworthy translator?"</P>
<P>In fact, a number of military officials at Guant&aacute;namo who served with Al-Halabi and Yee said that they would be surprised if either man was guilty of spying "in the traditional sense," according to ABC News. "These officials," ABC reported, "said it's most likely the men may have been trying to help contact inmates' family members or help the situation of the prisoners on a 'human level.'"</P>
<P>But to the Bush administration, the detainees at Guant&aacute;namo don't deserve humane treatment. U.S. officials still refuse to say when--if ever--any of the detainees will be put on trial. Most have been held in the camp for more than a year--and the U.S. seems prepared to keep them locked up indefinitely.</P>
<P>In a sickening twist, military personnel proudly showed an ABC reporter around Camp Iguana, the facility where three "JECs"--or "juvenile enemy combatants" are held. The three boys are between the ages of 13 and 15.</P>
<P>The military proudly points out that there is no barbed wire on the fences at Camp Iguana--and that in one section of the fence, the guards have even cut holes in the green mesh so that the boys can look out at the ocean. One soldier told the reporter that Camp Delta and Camp Iguana prisoners are being treated "well, so that eventually, when they get released back to their country, they know that Americans are generous and good people."</P>



