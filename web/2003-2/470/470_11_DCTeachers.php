<P><font face="Times New Roman, Times, serif" size="4">School board cancels plan to eliminate raise</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>D.C. teachers win gains</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Michele Bollinger</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 3, 2003 | Page 11</P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--The D.C. Board of Education voted last week to rescind their July decision to cancel the annual cost-of-living increase and the 9 percent raise due to teachers this fall. The vote was a victory for members of the Washington Teachers Union (WTU) who have put serious heat on the school board during the past two months.</P>
<P>Teachers were the only city workers to not receive the cost-of-living increase, and the cancellation of the 9 percent raise was a clear violation of the contract between the WTU and the D.C. Public Schools (DCPS). Teachers' anger at the cuts has been publicly directed at the board.  </P>
<P>Pressure has been so intense that the school board also voted to create, for the first time, an Ad Hoc Committee on Labor Relations. Teachers have mobilized to challenge the cuts in education and the violation of their contract by testifying at board meetings, threatening job actions and rallying outside board offices.  </P>
<P>Just this past week, the WTU Mobilization Committee--an active group of building representatives and rank-and-file teachers--voted to call a sickout for October 2 as well as to recommend that the WTU call an ongoing work-to-rule campaign. The mere rumbling about job actions forced the board to retract their cuts.</P>
<P>But the nature of last week's vote shows that teachers' organizing must continue. The board did not locate the funds to pay the raises, passing the buck on this question to the mayor and the city council.  </P>
<P>Most likely, teachers will not see the money for months--and perhaps not ever, unless they continue their fight. In an outrageous move, the board also called on the Superintendent of Schools to reopen the teachers' contract to renegotiate the 9 percent raise.  </P>
<P>This Tuesday, the WTU is joining with members of Teamsters Local 639 to rally for full funding for DCPS and DCPS workers. But teachers must continue the debate within the WTU about why and how they should organize job actions, such as a sickout, a work-to-rule campaign and a strike. It's clearer than ever that winning the 9 percent raise that teachers deserve will require a demonstration of teachers' power in the workplace.</P>


