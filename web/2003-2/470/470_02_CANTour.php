<P><font face="Times New Roman, Times, serif" size="4">Campus antiwar activists building for tour</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Speaking truth to empire across the U.S.</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 3, 2003 | Page 2

<P><font face="Times New Roman, Times, serif" size="3">IN OCTOBER, the Campus Antiwar Network (CAN) and the Muslim Student Association (MSA) will sponsor a nationwide campus speaking tour called "Speaking Truth to Empire." The tour will feature speakers such as Tariq Ali, Anthony Arnove, Medea Benjamin, Noam Chomsky, Rahul Mahajan, Rania Masri, Michael Parenti, Ahmed Shawki, Norman Solomon, Howard Zinn and others. MONIQUE JEANNE DOLS is a member of the Columbia University Antiwar Coalition in New York, and OMAR ANTAR is a member of the MSA at Columbia. They talked to <I>Socialist Worker</I> about the tour.</P>

<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><B>WHAT HAS been response of activists to the Speaking Truth to Empire tour?</B></P>

<p><font face="Times New Roman, Times, serif" size="4"><b>Monique</b></font><br>
<font face="Times New Roman, Times, serif" size="3">We decided on this focus because the issue of the occupation of Iraq is the pressing question in the minds of millions of people right now. Over the summer, the lies that Bush told to sell his war in Iraq became increasingly obvious to people. </P>
<P>With Bush asking for $87 billion for the occupation and more "sacrifices" from the American people while he decimates Iraq, I believe that more and more people are thinking to themselves, "Maybe those antiwar protesters were right." We said that Bush had no intention of freeing the Iraqi people, and the situation in Iraq today vindicates that.</P>
<P>The response has been enormous. At least 15 schools that don't yet have CAN chapters are looking to host the tour. We hope that this tour can help to widen our network of antiwar committees, as well as broaden the movement in general. The purpose of the tour is also to build the October 25 national mobilization against the occupation in Washington DC and San Francisco. </P>
<P>And coming out of the tour, our second national CAN conference is happening on November 1 and 2 in Chicago. This will give people a great opportunity to take the momentum from the tour towards organizationally shaping the direction of our movement on a national scale.</P>

<P><B>WHAT IS the significance of CAN and the MSA working together this fall?</B></P>

<p><font face="Times New Roman, Times, serif" size="4"><b>Omar</b></font><br>
<font face="Times New Roman, Times, serif" size="3">The MSA's collaboration with CAN is a continuation of the efforts that MSA did prior to the Iraq war. We hope to expand the groups with which we work to end the occupation and all forms of injustice, especially the overt imperialism advocated and implemented by the neo-cons of the Bush administration.</P>
<P>It's hard to determine how much "traction" the tour will give to the antiwar movement, but I do think the focus on occupation and imperialism is the correct focus, because that pinpoints the crux of the problem--i.e., the illegality of occupying other nations. </P>
<P>Rather than focusing on fixing the fringes of the problem--the need for a "better" occupation plan, more money or troops, involvement of the United Nations, etc.--we correctly focus on the fact that the whole notion of occupation is both illegal and immoral. </P>

<P><B>THERE a lot of debates about whether we can say "troops out now" and other issues in the antiwar movement. Do you expect these to come up through the tour?</B></P>

<p><font face="Times New Roman, Times, serif" size="4"><b>Monique</b></font><br>
<font face="Times New Roman, Times, serif" size="3">This tour is intended to contribute to the discussions going on right now inside the antiwar movement. After the U.S. declared victory in Iraq, our movement became unsure of what to do next. Today, I think that it is really important for those who stood against the war in the first place and those who are questioning it now to stand together to stop the occupation. In this sense, our movement is at a crossroads, and this tour is a contribution to the process of reorienting our struggle.</P>

<p><font face="Times New Roman, Times, serif" size="4"><b>Omar</b></font><br>
<font face="Times New Roman, Times, serif" size="3">The principle of self-determination should be upheld in all cases and without bias. Thus, the people of Iraq (and Palestine, etc) all have the right to determine their own government and future, despite the U.S. government's aversion [to] or bigotries against Islam and Muslims--or anyone else that wants to model their society in a way different from U.S. government wishes. If the Iraqis want a Shiite Islamic state, that should be their right, regardless of our views on the matter. </P>
<P>The UN has yet to demonstrate its independence from U.S. dictates, nor its justice towards the self-determination of peoples it purports to help, especially in the Muslim world. The need for the complete withdrawal of U.S. troops should be a given, but the question remains of how to make the transition to real self-determination, and not to a neo-con imposed puppet "governing council" or constitution. So the UN may be involved, but hopefully as little as possible.</P>

<P><B>WHAT CAN people do who want to get activism going on their campus?</B></P>

<p><font face="Times New Roman, Times, serif" size="4"><b>Monique</b></font><br>
<font face="Times New Roman, Times, serif" size="3">We are hoping that the CAN/MSA tour will help people to do just that by putting forward an activist-oriented anti-occupation message. People who are interested in hosting the tour should e-mail canspeakingtour@yahoo.com. We can help provide a military family member as well as a CAN speaker from a nearby school to come and talk about why we should oppose the occupation. And people should also start now to organize groups of people to come to our national conference in Chicago. This conference will be very exciting, and the only place where we can actually shape our own group.</P>

