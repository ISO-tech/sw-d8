<P><font face="Times New Roman, Times, serif" size="4">The super rich get super-richer while 1.7 million are...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Pushed into poverty</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 3, 2003 | Page 12</P>

<P><font face="Times New Roman, Times, serif" size="3">ANOTHER 1.7 million more people fell into poverty last year, and median family income declined. But the super-rich got even super-richer.</P>
<P>That was 2002 in George W. Bush's America--the second year of what economists claim is a recovery. The jobless recovery was the main reason for the spike in poverty rates to 12.1 percent--a total of 34.6 million people.</P>
<P>It's the second year in a row that the poverty rate has increased--the first time that's happened in more than a decade. Poverty hit African Americans hardest--with nearly one in four under the poverty line. More than one in five Latinos also were poor.</P>
<P>According to an analysis of Census Bureau poverty statistics by the Economic Policy Institute (EPI), a 2.5 percent drop in hours worked and slow wage growth were the main factors in the rise in poverty and the drop in median family income by 1.1 percent. Those at the bottom 10 percent of the income scale did far worse, suffering a decline of 4.2 percent in income.</P>
<P>But according to the <I>Wall Street Journal, </I>"Income inequality didn't worsen because the rich took a hit, too." Actually, income is only part of the calculation of net worth--which is why the wealthy few saw their bloated fortunes swell even more last year. According to <I>Forbes </I>magazine's annual survey, the 400 richest Americans saw their net worth rise 10 percent last year to an astonishing $955 billion.</P>
<P>And if the income statistics obscure the total wealth of these plutocrats, Census Bureau figures understate poverty, according to Jared Bernstein, an economist at the EPI. That's because government statisticians use a model of poverty first established in 1963, when food typically cost one-third of income.</P>
<P>Today, food is less expensive--but other costs such as child care and health care eat up a much bigger part of our paychecks. When these factors are taken into account, "you get a poverty rate that's 3 percent higher--a 15 percent poverty rate," Bernstein told <I>Socialist Worker. </I>"That's another 8.6 million poor people."</P>
<P>Bernstein said that U.S. poverty statistics based on income are similar to those in Western Europe--until transfer payments from social programs are taken into account, that is. Because countries like France and Germany have a national health insurance system and support for child care, workers and the poor can use their income to meet other needs. "The safety net is much more protected in those countries," Bernstein. "They go much further to reducing the poverty rates than we have."</P>
<P>Similar programs can be used to combat poverty in the U.S., he said. "Policymakers would have to get together and say, 'what can we do, not to please campaign contributors, but to reduce poverty numbers,'" he said.</P>
<P>Heading Bernstein's list are policies to create full employment--plus programs for health care, child care, transportation and wage subsidies. Those urgently needed programs aren't going to be handed out by Washington, of course. Rising poverty in the world's richest country should be cause for alarm and outrage--and an incentive to organize for action.</P>



