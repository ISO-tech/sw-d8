<P><font face="Times New Roman, Times, serif" size="4">Yale workers stand up against anti-union bosses</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Fighting obscene inequality</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 3, 2003 | Page 4

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I><br>
Last November, the British <I>Observer</I> published an article describing New Haven, Conn., as a textbook example of inequality in the U.S. "New Haven is a metaphor for America," states the article. "It is the country's fourth poorest city, where the ghetto laps at the walls of a university worth $11 billion in tax-exempt endowments, educating America's next generation of rulers. A sign at the freeway turn-off advertises New Haven as the birthplace of President George Bush.</P>
<P>"It is a city with the same infant mortality rate as Malaysia and a terrifying rate of deaths from AIDS--one day care center alone commemorated the loss of 600 clients at a memorial service on Wednesday. But it is located in America's richest state, Connecticut, which has, proportionally, more millionaires than any other."</P>
<P>In this sense, New Haven is the perfect example of the hopelessness that millions of ordinary people are feeling as they struggle to make ends meet while others live in luxury. But at the same time, workers in New Haven have set a different example--of how to fight back. </P>
<P>At a time when even traditionally militant unions are stepping back and taking concessions, Yale workers stood up to one of the most viciously anti-union universities in the country. While the gains made in the union's settlement are not enormous, they do constitute a victory at a time when bosses across the country are trying to break unions and force concessions. </P>
<P>Most importantly, the Yale workers won these gains because they fought for them, with the solidarity of thousands of workers across the country. At the "U.S. Labor Comes to New Haven" rally in September that drew more than 10,000 union members and activists, the message was clear: the Yale struggle is not just about Yale. </P>
<P>By standing in solidarity with the Yale workers and others who are fighting back, we are fighting for the future of ordinary people everywhere, and against the obscene inequality in which we are forced to live.</P>
<B><P>Leela Yellesetty</B>, New Haven, Conn.</P>


