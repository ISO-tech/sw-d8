<font face="Times New Roman, Times, serif" size="5"><b>New revolt spreading across Bolivia</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Tom Lewis</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 3, 2003 | Page 12</P>

<P><font face="Times New Roman, Times, serif" size="3">BOLIVIA'S MAIN social movements and largest labor confederation joined forces this week to confront the government of President Gonzalo S&aacute;nchez de Losada. The rebellion began in Bolivia's high plain, where Aymara peasant workers blockaded roads and demanded the right to self-determination.</P>
<P>Following the water war and coca protests of April and September 2000, the indigenous Aymara established a de facto region of autonomy in zones surrounding the towns of Achacachi and Warisata. They now intend to expand the area of self-government to reconstitute their ancestral home, the Great Omasuyos.</P>
<P>Government troops attacked the roadblocks on September 19. The next day, soldiers killed five people, including an 8-year-old girl, in the confrontations. Later that day, Aymara demonstrators shouted "Civil war! Civil war!" as peasant leader Felipe Quispe, head of the Confederation of Bolivian Peasant Workers (CSUTCB), declared an indigenous uprising.</P>
<P>Forces from the Coalition in Defense of Gas--a successor to the Coalition in Defense of Water and Life that successfully fought the U.S. multinational Bechtel's takeover of the water system in Cochabamba--vowed support for the peasant rebellion. More than 150,000 had rallied September 19 in Bolivia's major cities to protest the government's plan to let transnational corporations market Bolivia's petroleum and natural gas. Protesters demanded that these resources be used instead to modernize Bolivian industry and rebuild social services.</P>
<P>The government's plan would produce $1.3 billion in annual income for Pacific LNG--a consortium made up of Spanish-owned Repsol-YPF and British Gas and Panamerican Gas, a subsidiary of British Petroleum. The deal would return as little as $40 million annually to the Bolivian treasury in the form of taxes and fees.</P>
<P>The Bolivian Workers Confederation (COB) called for a general strike to begin Tuesday in support of the indigenous uprising and the Coalition in Defense of Gas. As <I>Socialist Worker</I> went press, it remained unclear how effective the strike would become. Leaders of the social movements--like Oscar Olivera, who came to be known worldwide for his role in the struggle against water privatization--believe that weeks of confrontation and struggle remain ahead.</P>




