<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Why Peter Camejo deserves your vote in California</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A real alternative in the recall circus</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 3, 2003 | Page 3

<P><font face="Times New Roman, Times, serif" size="3">THE REALITIES of California's recall election stood exposed last week during the 90 minutes of mayhem that passed for a debate between the top candidates to replace Gov. Gray Davis. In his only debate before the October 7 vote, media darling Arnold Schwarzenegger tried not to open his mouth--but couldn't help showing himself to be a celebrity front man for Republican business interests when he did.</P>
<P>With his patronizing disdain for Arianna Huffington, he undid weeks of carefully orchestrated efforts by his handlers to prove that he isn't a sexist pig. Incredibly, this Neanderthal managed to emerge with <I>more </I>support after the debate, according to a CNN/<I>USA Today/</I>Gallup poll released last weekend.</P>
<P>That's because his main opponent, Democratic Lt. Gov. Cruz Bustamante, is so pathetic. During the debate, Bustamante pretended to have nothing to do with the economic and political crisis that the state has suffered through during his years as second-in-command to Davis. But his tanking poll numbers show that people aren't buying it.</P>
<P>Huffington, meanwhile, is running a vanity campaign. Her support for charter schools and refusal to consider raising property taxes on the rich shows that she's not interested in building a real alternative to the status quo, as she claims.</P>
<P>Only Peter Camejo of the Green Party presented a case that California voters should listen to. He spoke out for universal health care and affordable housing, he opposed George Bush's occupation of Iraq and the Washington war machine, and he said the magic words that could solve the state's huge budget deficit--tax the rich. In an Internet poll in the <I>San Francisco Chronicle, </I>31 percent of people named Camejo the winner of the debate, the best showing of any candidate--a stunning result for someone who has been dismissed all along as irrelevant by the mainstream media.</P>
<P>Gray Davis still has a chance to survive next week's vote. Until last weekend, opinion polls showed support for the recall declining. This isn't because Davis has become more popular. He remains justly despised by millions of people for presiding over the state's budget crisis--and trying to make working people pay for it.</P>
<P>But voters have gotten a look at the leading contenders to replace Davis--and they don't like what they see. Ultimately, this election is an attempt by <I>both </I>Democrats and Republicans to escape their responsibility for California's crisis.</P>
<P>Voters in the recall election will first have to choose "yes" or "no" on whether Davis should be removed from office. A "no" vote clearly would be a vote to keep Davis as governor. But the main forces pushing for a "yes" vote--and the main beneficiaries of Davis' removal from office--are the Republicans. With either result, ordinary Californians get the shaft.</P>
<P>That's why we call for <I>Socialist Worker </I>readers in California to abstain on the recall question. But there <I>is </I>an alternative on the second part on the ballot--who should replace Davis if he is removed.</P>
<P>Peter Camejo has spoken out for a real alternative from the status quo presided over by the mainstream parties. We believe that he deserves our readers' vote.</P>
<P>And California voters should also oppose the racist Proposition 54--the deceptively titled Racial Privacy Initiative, which would harm efforts to address discrimination by barring the state government from collecting information about race and ethnicity.</P>
<P>Camejo isn't likely to become the next governor of California on October 7. But a substantial vote for him will send the message that people want more than a choice between the two sets of misleaders who drove the state into a ditch--and who want ordinary people to pay to get it out.</P>

