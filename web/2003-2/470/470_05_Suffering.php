<P><font face="Times New Roman, Times, serif" size="4">Pentagon quiet about high casualty rate</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Suffering for oil and empire</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 3, 2003 | Page 5

<B><P><font face="Times New Roman, Times, serif" size="3">NICOLE COLSON reports on U.S. soldiers' growing questioning of the U.S. occupation of Iraq.</P>
</B>
<P>NEVER ONE to miss a photo op, George W. Bush spent part of September 11 this year visiting wounded U.S. soldiers at the Walter Reed Army Medical Center in Washington, D.C. "I was able to pin the Purple Heart on a number of people upstairs," he bragged to reporters. </P>
<P>But the mainstream media mostly failed to ask the real question: Why were there so many soldiers for Bush to pin medals on? Pentagon officials regularly brag that the number of U.S. war dead in Iraq is low compared to past interventions.</P>
<P>Soldiers' body armor equipment and advances in medical care have reduced the number of fatalities. But lost amid the media hype is the high rate of wounded and injured--many of them severe.</P>
<P>According to the <I>Washington Post</I>, the number of U.S. troops wounded in action in Iraq is double that of the 1991 Gulf War. More than 6,000 service members have been flown back to the U.S. for medical reasons since the war began.</P>
<P>At least 1,100 of those were "wounded in action," according to the Pentagon, and more than 300 were sent home as a result of "non-hostile injuries" in vehicle or other accidents. Hundreds more are ill with diseases such as pneumonia and hepatitis. And others have been sent back with mental health problems--usually caused by combat stress. As a result, Walter Reed has been filled nearly to capacity for weeks--and officials have been forced to refer some patients to area hotels.</P>
<P>The severity of the combat injuries and the high number of soldiers who had limbs amputated because of grenade and bomb attacks has surprised Jim Mayer, a double amputee from the Vietnam War, who volunteers at Walter Reed. "A few of us started volunteering as amputees in 1991, and this is the most we've seen ever," Mayer told the <I>Post</I>. "I've never seen anything like this.</P>
<P>Beyond the physical casualties, the Pentagon rarely gives any hint of the stress, fatigue and low morale that troops are suffering. Yet there have been a dozen or more suicides among U.S. troops in Iraq, and Major Ernie Proud, the clinical officer in charge of the Army's 113th Combat Stress Control Team, recently told ABC News that he is surprised by how many units have been requesting sessions in suicide prevention, anger management and stress management.</P>
<P>Don't expect the mainstream media to pay attention any time soon. "Dead people's names can be posted on a television honor role," antiwar columnist Norman Solomon recently told WorkingForChange.com, "but the networks and cable news channels won't clog up their air time with the names and pictures of hundreds and hundreds of wounded soldiers."</P>

<p><font face="Times New Roman, Times, serif" size="4"><b>What would $87 billion buy?</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">GEORGE BUSH sent a huge bill to Congress last month, asking taxpayers for $87 billion to fund the occupation of Iraq and Afghanistan next year. Instead of funding an occupation for oil and empire, what could that $87 billion be used for? </P>
<P>--Pay off every single state government budget deficit in the U.S.</P>
<P>--Give nearly two years' worth of unemployment benefits to everyone currently on the rolls</P>
<P>--Give $26,363 to each one of the 3.3 million people who have lost their jobs since Bush took office </P>
<P>--Hire more than 2 million new teachers</P>
<P>--Spend an additional $1,824 on every child in U.S. public schools </P>
<P>--Provide more than three years' worth of funding at current levels for low-income schools, after-school programs, special education programs and college Pell grants </P>
<P>--Spend $7,909 on each child in the U.S. without health insurance</P>
<P>--Increase the Environmental Protection Agency's budget more than tenfold</P>
<P>--Spend $226,029 on every AIDS patient in the U.S.</P>
<P>--Fulfill the president's promise of funding for the AIDS epidemic in Africa this year--and have enough left over to make a similar commitment for 28 years to come.</P>



