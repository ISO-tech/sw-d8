<P><font face="Times New Roman, Times, serif" size="4">Freedom ride for immigrant workers:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Get on the bus!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mike Estrada, Evan Kornfeld and Ellie Fingerman</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 26, 2003 | Page 12</P>

<P><font face="Times New Roman, Times, serif" size="3">THOUSANDS OF people turned out in West Coast cities September 20 to send off buses traveling across the country for the Immigrant Workers Freedom Ride. Modeled on the Freedom Rides that challenged segregation in the South in the 1960s, the AFL-CIO initiated the campaign to build grassroots support for the fight for civil rights and legal protection for immigrant workers. </P>
<P>In the coming weeks, the buses will travel to dozens of U.S. cities for rallies and pickets--and meet up on the East Coast for two major demonstrations in Washington, D.C., and New York City in early October.</P>
<P>In San Francisco, some 2,500 workers, students and community members gathered for the send-off rally. "People need to get together, to march, to protest," said Adrian Martinez, a legal immigrant from Mexico City with Laborers union Local 304. Immigrants need the help of others. It's hard for immigrants to unionize."</P>
<P>Rev. Forest Gillmore from Monterey County, who's riding in the caravan, echoed this sentiment. "It is important to get people out," Gillmore said. "Through organization of people, we have power. Without them, we have nothing."</P>
<P>The San Francisco event mirrored the diverse support for the campaign. Community groups such as the SF Day Laborers Program and Mujeres Unidas y Activas turned out, as did Latino student groups and the local chapter of U.S. Labor Against the War. The Laborers mobilized more than 500 members. The Service Employees International Union, Hotel Employees and Restaurant Employees, AFSCME and the International Longshore and Warehouse Union also turned out.</P>
<P>The AFL-CIO's sponsorship of the Freedom Ride campaign is a tremendous step forward for the federation, which has in the past has catered to backward anti-immigrant policies. The initiative reflects the labor movement's growing awareness that immigrants are an important part of the workforce who need the protection of unions.</P>
<P>"Immigrants are being taken advantage of," said Leonard Rosen of AFSCME Local 3299. "It isn't a coincidence that people who are anti-labor are anti-immigrant, too." Rev. Gillmore agreed. "Big business has taken advantage of poor whites' prejudice," he said. "We want to turn that around. Poor whites are being screwed by the powers that be, and we want to help them to recognize how they are being exploited, too."</P>
<P>Other protesters at the San Francisco rally spoke out against the arbitrary roundup and deportation of immigrants in the guise of "national security" following the September 11 attacks. We are trying to send the message that workers aren't 'terrorists' and that they deserve respect," said Freedom Rider Isela Diaz of Sacramento.</P>
<P>In Los Angeles, some 2,000 rallied in a send-off for the Freedom Riders, and 300 people turned out in Seattle. Chants of "&#161;Si, se puede!" (Yes, we can!) rang through the large union contingents in Los Angeles. "We're not begging, we're not on our knees, we're not even asking any more," said United Farm Workers President Arturo Rodriguez. "We are demanding our rights as workers."</P>
<P>Speakers took aim at the Bush administration. "Mississippi is the model that Bush and Ashcroft want for the U.S," said Rev. James Lawson, who took part in the Freedom Rides of the 1960s. "We must say to George Bush, 'This land is not yours. If you can spend $87 billion on Iraq, you can spend $87 billion on the poor and on decent jobs.'"</P>
<P>A number of Democratic politicians were on hand for many of the events. But it would be a serious mistake if activists let them off the hook. For example, Democratic Gov. Gray Davis, who appeared at the San Francisco rally, is responsible for cutting state social services and jobs, particularly in education.</P>
<P>An active labor movement that looks to rank-and-file organizing can build the kind of opposition we need to Bush's policies--whether they come from Republicans or Democrats. Armondo--a Guatamalan restaurant worker who came to the U.S seeking political asylum and started his freedom ride in Seattle--summed up this spirit of organizing.</P>
<P>"I'm doing this because I believe in justice." he told <I>Socialist Worker.</I> "This is about raising consciousness. And whoever doesn't consider themselves an immigrant needs to think back. We are making history. I am doing this because I realized I can make a difference."</P>



