<p><a name="Green"></a><font face="Times New Roman, Times, serif" size="4"><b>Green light for cops to commit perjury</b></font></P>

<B><P>Dear <I>Socialist Worker,</I> <BR>
</B>In August, a crowd of more than 200 gathered outside the State of Illinois Building to hear political and religious leaders criticize Gov. Rod Blagojevich's veto of Illinois' death penalty reform bill. Blagojevich vetoed the bill because it contained a measure that would allow a cop to be fired for committing perjury in a murder case.</P>
<P>Just last January, outgoing Gov. George Ryan pardoned four death row inmates convicted on the basis of confessions extracted through torture, and commuted all of the sentences of Illinois' death row inmates, finding the police torture victims as the most egregious example of the system's flaws.</P>
<P>Rally organizers repeated that they were not against the "good" officers, just the "bad" ones. But the police torture ring, headed up by fired Police Commander Jon Burge, set an example that many non-Burge officers followed--leading to hundreds, maybe thousands, of wrongful conviction cases based on perjured testimony. This is not a case of a few bad apples, but a systematic problem involving the Chicago Police Department and the State's Attorney's office as a whole.</P>
<P>One former Illinois death row inmate had a lot to say about Blagojevich's veto. "He signed the bill to video all murder confessions--knowing that it leaves room for officers to lie and do all kinds of stuff to get around the mandatory taping," the prisoner said. "Then he tells them it's okay to commit perjury, to wrongfully convict someone--and you can do it without losing your job. He vetoed a bill that would have a cop fired for committing perjury in murder cases--when other people go to jail for committing perjury. One of the most serious problems with the system is that prosecutors and officers don't fear repercussions for lying, misconduct and abuse."</P>
<P>It looks like anti-death penalty legislative and religious leaders aren't going to back down, but will continue to put pressure until the veto is repealed--and Illinois cops can be held accountable for their misdeeds. This was a great first step.</P>

<B><P>Joan Parkin, </B>Chicago</P>



