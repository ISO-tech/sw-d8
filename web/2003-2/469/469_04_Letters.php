<p><a name="Consume"></a><font face="Times New Roman, Times, serif" size="4"><b>What we buy isn't the real problem</b></font></P>

<B><P>Dear <I>Socialist Worker,</I> <BR>
</B>In her letter "Consumers share the blame" (<I>SW</I>, September 5), A. Heather Williams turns the real argument on its head. Her position that the market takes its cue from consumers, or that consumption determines production, is the reverse of the way it really works. </P>
<P>SUVs, for instance, didn't appear on the market in response to some consumer poll. They are the result of the competitive nature of the capitalist system. In order to stay ahead of their competitors, auto manufacturers must gain more profit than their rivals. This means, among other things, expanding their market: finding new people to buy their products, making new products to sell or finding new ways to sell their existing products.</P>
<P>Things like fuel efficiency only come into play in so far as they fit into the sales pitch. Real decisions about what gets produced and how are made by CEOs in boardrooms, not by the workers who actually produce the goods and services--or by the people who buy them.</P>
<P>Not only are the decisions made at the top, but that's also where the vast majority of the wealth that workers produce winds up as well. So to say "we're all to blame" not only lets the fat cats off the hook; it misses a fundamental reality. To simply say that billionaire CEO Bill Gates and minimum-wage-worker John Doe are both "consumers" and are therefore on a level playing field is ludicrous.</P>
<P>People like Bill Gates <I>do</I> consume too much--and have the resources to make whatever kind of purchasing decision they please. The rest of us have to do as best we can with the options and resources available to us. </P>
<P>To truly challenge the system and build a better world where production is used for human need instead of profit, we need to organize at our strongest point (where we produce), not at our weakest (where we consume). That's as workers using our combined strength in struggle--not as individual consumers at the mercy of the market. </P>

<B><P>Roger Dyer, </B>San Francisco</P>

