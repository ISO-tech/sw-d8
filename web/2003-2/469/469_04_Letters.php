<P><p><font face="Times New Roman, Times, serif" size="4"><b>Exposing the lies Blair used for war</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</I> <BR>
</B>British Prime Minister Tony Blair--George W. Bush's loyal lapdog in Europe--suffered a humiliating backlash from the war in Iraq last week. In a parliamentary bi-election in Brent East in London, Blair's "New" Labour Party saw a majority of 13,047 at the last election turn into a 1,100 majority for the Liberal Democrats. </P>
<P>This is one of the largest turnarounds in British political history. The Liberal Democrats are seen by many as being antiwar (despite them backing the troops once the bombing started). Senior Labour politicians were clearly rocked by the result--and there is open talk of Labour losing the next general election. </P>
<P>It seems that every week an ex-Cabinet minister speaks out, calling for Blair to go. Blair's weakness is clearly the result of the massive antiwar movement which saw 2 million march in London on February 15, and will see another huge turnout on September 27, calling for an end to the occupations of Iraq and Palestine. </P>
<P>Our campaigns have exposed every lie that Blair told to justify his support for Bush. It should give confidence to U.S. campaigners who want to see Bush Jr. go the same way as his daddy.</P>

<B><P>Clive Searle, </B>Socialist Alliance, Manchester, UK</P>

