<P><font face="Times New Roman, Times, serif" size="5"><b>Bush's ally in Italy admires Mussolini</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 26, 2003 | Page 2

<P><font face="Times New Roman, Times, serif" size="3">ITALY'S PRIME Minister Silvio Berlusconi set off a furious controversy last week when he defended Benito Mussolini, the country's fascist dictator during the Second World War. Berlusconi, one of George W. Bush's main allies in Europe and a key supporter of the war on Iraq, said in an interview, "Mussolini never killed anyone. Mussolini used to send people on holiday in internal exile."</P>
<P>The smug comment about sending people "on holiday" was apparently a reference to Mussolini's practice of banishing some political opponents to prison camps on small Italian islands that are now fancy resorts. But the dictator is notorious for his long record of murder--from the brutal occupation of Ethiopia in the mid-1930s to collaborating with Germany's Nazis by rounding up Italian Jews for Hitler's concentration camps.</P>
<P>Berlusconi's comment is no surprise. He is a right-wing media baron whose government depends on the support of far-right parties like the Nazi National Alliance, which claims to be "post-fascist." And this is the company that George Bush keeps in Europe.</P>


