<P><font face="Times New Roman, Times, serif" size="5"><b>Yale strike wins gains for workers </b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Naveen Jaganathan</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 26, 2003 | Page 11</P>

<P><font face="Times New Roman, Times, serif" size="3">NEW HAVEN, Conn.--The 23-day strike at Yale came to a close September 19 with a mixed victory for the unions, including major gains in pensions. The deal ends a 19-month contract battle between Yale University and Hotel Employees and Restaurant Employees (HERE) Locals 34 and 35. Workers at the Local 34 contract ratification meeting walked in chanting "V-I-C-T-O-R-Y." </P>
<P>Meanwhile, Yale President Richard Levin told the <I>New York Times </I>that the unusually long eight-year contract is a "victory" for the school. In fact, Levin had to give ground on key union demands after the unions' noisy picket lines, demonstrations and civil disobedience disrupted university life.</P>
<P>The contract offers workers in Local 34 (clerical workers) a total of a 37 percent increase in wages over 8 years. It provides Local 35 (maintenance and food service workers) a 28.5 percent increase in wages over the same period.</P>
<P>While these gains are ahead of most recent settlements, the unions failed to win full retroactive pay for the 18 months that they've worked without a contract. The biggest gains for the unions were in pensions, which will double for most workers. </P>
<P>Going into the strike, Yale offered a 1.25 percent multiplier on the pension funds, compared to the 1.95 percent multiplier demanded by the unions. The contract has settled for a 1.5 percent multiplier for those who earn up to $30,000, 1.4 percent multiplier for the next $25,000, and a 1.3 percent multiplier for those earning over $55,000.</P>
<P>HERE President John Wilhelm told the <I>New York Times</I>, that he was "very pleased with [the contract]." The fulcrum of the agreement is the union agreeing to an unusually long-term contract, which enabled the university to significantly improve some of the terms, particularly the pensions. That's a good exchange.</P>
<P>In fact, the eight-year deal--which is retroactive to the expiration of the last contract in January 2002--favors management. It not only preserves the "labor peace" that Levin wants until 2010, but puts the other unions engaged in a battle with Yale in a weaker position.</P>
<P>The teachers'assistant and research assistants' union, GESO, and the hospital workers, who aim to organize Yale New Haven Hospital under SEIU 1999, will now have to take on Yale separately for years. As a result, the 150 hospital workers plan to end their own strike without a contract.</P>
<P>The main problem for HERE was their inability to organize more of the clerical workers in Local 34 to go on strike. About a third of the clerical workers crossed picket lines. Nevertheless, members of Local 34 and 35 are sure about one thing: Without the strike they wouldn't have won the decent pension plans and job-security language they sought, even if the deal fell short in other respects.</P>
<P>With HERE's contract battle over, the battle to unionize hospital workers and graduate student workers will continue. And these union contract battles with Yale are part of a much larger struggle for justice waged by the New Haven community against the university. The HERE strike set an important precedent for that fight.</P>




