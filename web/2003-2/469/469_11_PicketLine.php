<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 26, 2003 | Page 11</font></P>

<p><font face="Times New Roman, Times, serif" size="4"><b>Washington, D.C., teachers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jesse Hagopian, Washington Teachers Union</b></font></p>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--More than 2,500 enraged public school teachers rallied outside the school district headquarters last week to put muscle behind their contract demands. Teachers' anger has boiled over after several months of stepped-up attacks by the D.C. School Board, including the imposition of longer work hours at the end of last year, the slashing of teacher's annual wage step increase, and most importantly, their reneging on teacher's 9 percent pay raise for the 2003-2004 school year promised in the existing contract.</P>
<P>Teachers at the rally packed the sidewalk waving picket signs as passing cars honked their horns. The teachers' demands are simple--honor the contract, no to school vouchers, and "no 9 percent October 1, no work October 2!" At one point during the rally, enthusiastic teachers spilled out into the street, causing the police to physically restrain the protesters and resulting in the arrest of one non-teacher supporter.</P>
<P>The rally marked a significant step forward for the Washington Teachers Union (WTU). A previous rally in June had just half the number of teachers, proving to everyone that there has been an escalation in organization. It is now clear that a majority of the 5,000 teachers in the WTU favor job actions, from sickouts to strikes, if the Board doesn't come up with the money for the 9 percent salary increase by the promised October 1 deadline.  </P>
<P>"We're not asking anymore. We are telling you, we want our 9 percent," said one teacher from the podium. "And let this be a warning that the teachers are prepared to do what is necessary to win!" Following the rally, teachers packed the School Board meeting that night to make their demands.  </P>
<P>We need to unify against all the city officials and their backwards priorities--and demand a total restoration of teachers and other school workers' raises and a reinstatement of laid off positions immediately. If teachers are to win this fight, we are going to have to do more than rally and lobby the School Board--we are going to have to be prepared to walk off the job to take what is rightfully ours.</P>


