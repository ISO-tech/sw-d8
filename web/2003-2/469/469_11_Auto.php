<P><font face="Times New Roman, Times, serif" size="4">UAW leaders agree to contract that slashes jobs</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Big givebacks in auto deal</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 26, 2003 | Page 11</P>

<P><font face="Times New Roman, Times, serif" size="3">THEY'RE THE biggest concessions for the United Auto Workers in 20 years--and if approved, they'll haunt the UAW and the entire labor movement for many years to come. While UAW President Ron Gettelfinger is trumpeting success in maintaining most health care benefits, the proposed deal would eliminate tens of thousands of jobs--an estimated 50,000 at General Motors (GM) alone.</P>
<P>In the name of labor-management partnership, the UAW has abandoned its 66-year-old principle of equal pay for equal work, agreeing to allow top parts suppliers to introduce a lower-tier pay scale for new hires of $14 to $16 an hour--compared to the current $24 to $26 range. "They're taking this union back to where we were in 1933, when we had company unions," said David Yettaw, former president of UAW Local 599 at GM's Buick City complex in Flint, Mich.</P>
<P>In fact, negotiations on the lower-tier wage levels for parts plants won't even be finalized for another 90 days--after the expected ratification votes. But well before the two-tier deal is finalized, workers at GM and the company's former parts division, Delphi, will vote on a combined contract. "If we could vote separately, we could sink it, but there are three or four times as many GM workers [as] at Delphi," said Gregg Shotwell, an activist in the UAW Solidarity Coalition and a member of UAW Local 2151 at Delphi's plant in Cooperstown, Mich.</P>
<P>Two-tier wages are also set for Ford's former parts division, Visteon, where the UAW has already allowed lower-paid workers to be hired in new plants. For its part, DaimlerChrysler will sell or close up to nine parts plants. Wages in the surviving parts plants will almost certainly be cut, as they were at a former Chrysler parts plant in New Castle, Ind.</P>
<P>So after repeated failures to organize assembly plants owned by Honda, Nissan and Toyota, the UAW's strategy is to sell the union to employers by helping them become more competitive. "If we want to keep manufacturing jobs in the U.S., we can't be fighting management...or we'll end up seeing more jobs go overseas," Bob King, the UAW vice president for organizing, told reporters.</P>
<P>To that end, national UAW negotiators agreed to the elimination of job classifications during management's plant-level negotiations with UAW locals. "What it would mean is that management could put anyone anywhere at any time," said Gene Austin, shop committee chair of UAW Local 594 at GM's truck assembly plant in Pontiac, Mich., which has 46 job classifications.</P>
<P>The UAW will also help management squeeze workers with reduced numbers of absences and harsh new penalties, including termination. Nate Gooden, the UAW's top negotiator with DaimlerChrysler, explained the new policy:<I> </I>"You will be discharged. You will not return to DaimlerChrysler. Because we are sick and tired of taking care of you."</P>
<P>The deal also contains weak pay increases for assembly plant workers--117,780 at GM, 77,460 at Ford, and 57,490 at Chrysler. It includes a $3,000 lump-sum payment in the first year, another lump sum worth 3 percent of wages the following year, and pay raises of 2 percent and 3 percent in the final two years--total compensation worth just half as much as the last contract. By accepting lump sums rather than wage increases, the UAW would lower the base for future wages and benefits in the future.</P>
<P>There are also givebacks in heath care, including the doubling of co-payments for brand-name drugs up to $10.50. Rather than boost pensions for the 363,500 retirees and their survivors, the deal would provide annual cash bonuses of $800, plus $1,000 vouchers for new vehicle purchases in the first and third year of the contracts.</P>
<P>Management and UAW leaders hope that the lousy pay gains will be enough to buy a "yes" vote from current workers at the expense of retirees and lower-paid future hires. A "no" vote would send a message that the UAW rank and file still upholds the tradition of solidarity that their leaders have rejected--and mark a major step towards rebuilding a fighting union.</P>



