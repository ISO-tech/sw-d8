<p><a name="Zionists"></a><font face="Times New Roman, Times, serif" size="4"><b>Protest Ehud Barak and Tommy Franks</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By John Green</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">SACRAMENTO, Calif.--Two out-of-state killers visited Sacramento on September 19. Former Israeli Prime Minister Ehud Barak joined retired U.S. Army General Tommy Franks at an exclusive $185-a-person forum that also included right-wing comedian Dennis Miller. </P>
<P>But local activists didn't let them off the hook. Thirty-five protesters assembled to oppose them. "Sacramento is not honored to have both of these criminals here," Isma'il Kamal told <I>Socialist Worker.</P>
</I><P>Conservatives in the crowd jeered antiwar activists and even tried to provoke a fistfight. But protesters like Tu Huynh were undeterred. She simply smiled and said, "I'm here to represent the underrepresented." Peace Action organized the protest, along with Code Pink and Students for Justice in Palestine.</P>


