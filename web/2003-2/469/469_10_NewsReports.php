<P><p><font face="Times New Roman, Times, serif" size="4"><b>Stop the cuts to veterans' benefits</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Thomas Barton</b></font><FONT FACE="Verdana"> </P>
</FONT><P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Two hundred angry veterans rallied at the Manhattan Veterans Administration (VA) Hospital on September 16 to protest proposed cuts in services.<FONT FACE="Verdana"> </FONT>The government wants to close outpatient treatment at the Manhattan and Bronx VA hospitals, forcing disabled and sick vets to make long trips to a Brooklyn facility. The plan also calls for 378 in-patient hospital beds at the Manhattan facility to be eliminated in the name of "efficiency."<FONT FACE="Verdana"> </P>
</FONT><P>While veterans of the Second World War, Korea and the first Gulf War manned a block-long picket line, a group of about 20 Vietnam veterans dressed in their old war fatigues and boonie hats took it to the street. Setting up a checkpoint that blocked two of the four lanes on 23rd Street, they held up signs and demanded passing traffic honk in support of the vets.</P>
<P>The response was deafening. Taxi drivers shouted and blew their horns. Drivers of huge semi-trailers launched earsplitting blasts on their air horns. Bus drivers and vets exchanged clenched-fist salutes. Unhappy-looking police looked on but decided not to intervene.</P>
<P>"Where is Schumer? Where is Hillary?" a Gulf War veteran named Ralph asked <I>Socialist Worker. </I>"When they want the votes they promise you anything, but where are they today? They don't give a shit!"</P>
<P>"This is disgusting, this is such a shame," said Jose Rodriguez, a Local 420, District Council 37 member from Bellevue Hospital. "They go to war, they come back hurt and this is what they get. Now they want $87 billion for this war, and they cut services for the wounded veterans? What's that?"<FONT FACE="Verdana"> </P>

