<p><a name="Wolfy"></a><font face="Times New Roman, Times, serif" size="4"><b>Protest Paul Wolfowitz</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Peter LoRe</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--"War Criminal! You're a war criminal!" shouted activists protesting a visit by Deputy Defense Secretary Paul Wolfowitz at the New School on September 21. Wolfowitz is an architect of the war on Iraq and a prominent member of the right-wing think tank Project for the New American Century, which mapped out plans for pre-emptive war and regime change.</P>
<P>Throughout the event, Wolfowitz was repeatedly heckled with shouts of "Where are the weapons?" During a brief question-and-answer period, audience members grilled Wolfowitz. "I can't stand the Bush administration," said protester May Sha. "It drives me mad that they think they can get away with this." It's time to revive the antiwar movement and stand together against the liars in the White House.</P>

