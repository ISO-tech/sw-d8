<P><font face="Times New Roman, Times, serif" size="4">Pentagon preys on undocumented immigrants as...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Cannon fodder in Bush's war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 26, 2003 | Page 12</P>

<P><font face="Times New Roman, Times, serif" size="3">JUAN ESCALANTE was good enough to fight for the U.S. government in Iraq. But he's not good enough to be a U.S. citizen--as far as the Bush administration is concerned. That's because the 19-year-old Army private is an undocumented immigrant. </P>
<P>Juan came to the U.S. with his parents when he was 4 years old, and after graduating from high school, he bought a fake green card and joined the Army. He wanted to leave home so that he could take some of the financial burden off his family, since his mother and father both work full-time--and his father also works a second job at night--to support Juan and his little brother and sister.</P>
<P>Now, however, the U.S. military may discharge him--and immigration authorities are threatening to deport Juan and his parents to Mexico. Why? Because while Juan was stationed in the Gulf, his parents petitioned to become legal residents--and were denied. Incredibly, the judge said they failed to show how being sent back to Mexico would cause hardship for Juan's little brother and sister. </P>
<P>The case is a slap in the face to the thousands of immigrants who shoulder some of the heaviest burdens in the military. Currently, there are more than 37,000 non-citizens in the active-duty military--and more than 3,000 have served in the war against Iraq. </P>
<P>But the sickening truth is that their best chance at citizenships is to die in combat.</P>
<P>That's because last year Bush signed an executive order making it easier for families of foreign nationals killed in action to apply for citizenship--and for citizenship to be awarded posthumously to those killed in the "war on terror." But Juan's case shows that if you come back alive, you aren't worth keeping in the military--or even the country.</P>
<P>Latinos in particular are seen by the Pentagon as a lucrative "market" for military recruitment. According to a recent report in Britain's <I>Independent </I>newspaper, "Senior Pentagon officials have identified Latinos as by far the most promising ethnic group for recruitment, because their numbers are growing rapidly in the U.S., and they include a plentiful supply of low-income men of military age with few other job or educational prospects."</P>
<P>This is an admission of a widely acknowledged fact--that there's a poverty draft in the U.S., with the bottom ranks of the U.S. military filled by young men and women who join up because of the promise of job training and money for college. In fact, the Pentagon admits that recruitment is up <I>because</I> the economy is so bad.</P>
<P>"That's the driver, the economy," Major Gen. Michael Rochelle, head of the Army Recruiting Command at Fort Knox, told the <I>New York Times. </I>"[Recruits] are seeing the facts and the world situation, as well as the domestic implications of the economy, job opportunities and prospects, and the opportunity for higher education, which are impacted by rising tuition costs."</P>
<P>Latinos, who suffer poverty and unemployment in higher numbers than whites, are particularly vulnerable to the Pentagon's pitch. It's even been reported that Army recruiters have traveled to Tijuana, Mexico, to recruit young men eligible for entry into the U.S. </P>
<P>To sweeten the deal, the Bush administration has told immigrants who join the military that they can apply for citizenship the day they join up--instead of having to wait the usual five years after receiving their green card. This is outright bribery.</P>
<P>Underneath the "support our troops" rhetoric, Washington's war makers see young men and women like Juan Escalante as nothing more than cannon fodder for their war for oil and empire. We have to stand up to this outrage!</P>




