<P><font face="Times New Roman, Times, serif" size="4">Partners in crime at home and abroad</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Is there anything they don't lie about?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 26, 2003 | Page 1

<P><font face="Times New Roman, Times, serif" size="3">THE LIARS in the White House have told so many whoppers that they can't keep their stories straight. Two weekends ago, Dick Cheney appeared on NBC's <I>Meet the Press </I>and claimed that Saddam Hussein might have had connections to the September 11 attacks. "We learned more and more that there was a relationship between Iraq and al-Qaeda that stretched back through most of the decade of the '90s," Cheney said.</P>
<P>The very next day, George W. Bush said that there was no link after all. "We've had no evidence that Saddam Hussein was involved in September 11," Bush said.</P>
<P>Cheney, in the same interview, declared that he had severed all ties to the oil company Halliburton, where he was CEO until joining the Bush team. Lo and behold, the next day, the press reported that Cheney has pocketed more than $309,000 in "deferred compensation" from Halliburton since he's been in office. And Halliburton, by the strangest of coincidences, has raked in more than $1.7 billion in Pentagon contracts to "reconstruct" Iraq.</P>
<P>This week, Bush went to the United Nations (UN) to defend the U.S. invasion of Iraq--and ask for military and economic help to keep the disastrous occupation going. The lies that he and his pals have been telling for months to justify the invasion have been exposed--Iraq's nonexistent "weapons of mass destruction," the nuclear threat, the links to al-Qaeda. And the stage-managed pictures of celebrating Iraqis following the fall of Saddam Hussein's government have given way to massive resistance to Iraq's new colonial rulers.</P>
<P>But Bush would "acknowledge no mistakes in planning for postwar security and reconstruction in Iraq," the New York Times reported. No mistakes? Tell that to the millions of Iraqis still going without electricity and clean water.</P>
<P>Tell that to the families of those killed by U.S. occupation forces--or the victims of the chaos and violence that has plagued Iraq since the U.S. took over, claiming an incredible 1,000 Iraqi lives each week, according to British journalist Robert Fisk. Tell that to tens of thousands of U.S. soldiers who grow increasingly bitter at being ordered to carry out an occupation for oil and empire.</P>
<P>There's no lie too outrageous for Bush and his gang to try out. No wonder more and more people are fed up with them. Support for Bush has fallen from its postwar heights to below where it was prior to the September 11 attacks, according to opinion polls. Now is the time to organize to stop Bush and his fellow war makers in Washington.</P>



