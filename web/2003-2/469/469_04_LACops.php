<P><font face="Times New Roman, Times, serif" size="4">Killed by LA cops for playing a stereo too loudly</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>We demand justice!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 26, 2003 | Page 4</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><B>Dear<I> Socialist Worker,</B></I><br>
The police killed two people from my school in one week.</P>
<P>Raphael Ramos, a ninth-grade student, was crossing the street in the crosswalk with his friend Ricardo while walking home from school. An officer from LA's Southeast Division, driving alone in his squad car, struck the two students. Ricardo survived, but Raphael died of kidney injuries and massive head trauma.</P>
<P>Days later, Jeffrey Watts, a custodian at our school and a friend to many teachers, was murdered in his driveway by the San Bernadino County Sheriffs. Jeffrey, who had a passion for rock and roll, was playing music loudly in his truck at 2 p.m. Police arrived and told Jeffrey to get out of his truck. </P>
<P>He was schizophrenic and had a fear of authority. The facts are disputed, but he either stayed in the truck, or got out and started to walk toward his house. He was pepper-sprayed, beaten by four cops and put in a chokehold. He was handcuffed on his way to the hospital, where he died an hour later. </P>
<P>Tragically, Jeffrey's mother was going to the hospital for surgery on her cancer three days later. "I'm not afraid to speak out," his father told me. "Murdering my son for a noise infraction doesn't ring right with me." </P>
<P>The family has hired a lawyer and will probably hold a press conference next week. Our school is mourning the loss of two people who were remembered fondly. </P>
<P>Our chapter of United Teachers Los Angeles has put out a statement to the press to counter the biased reporting on these two events, and to demand answers. We are looking for ways to support the families' efforts for justice. The students are planning on inviting the press to a memorial service that they'll hold at school.</P>
<B><P>Sarah Knopp</B>, Los Angeles</P>

<I><P>Messages of advice or support for the families can be sent to slknopp@att.net.</I></P>

