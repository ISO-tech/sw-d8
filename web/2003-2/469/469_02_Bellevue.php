<P><font face="Times New Roman, Times, serif" size="4">New York City hospital workers fear building's collapse</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Is Bellevue falling down?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 26, 2003 | Page 2

<P><font face="Times New Roman, Times, serif" size="3">WORKERS AT Bellevue Hospital in New York City--the oldest public hospital in the U.S.--are worried that one wing of the building is falling down. A big crack has opened up in the outside wall of the hospital's CD wing where it joins the main building, running from the top of the seven-story building to the second floor. The apparent cause is a construction project next door to expand the hospital.</P>
<P>Despite its age, Bellevue is a mainstay of the public health system in New York. Expansion is desperately needed to help serve poor New Yorkers who have no other access to health care. But the politicians won't spend the kind of money needed to really fix up public health facilities--preferring instead to hand out tax breaks to Corporate America.</P>
<P>The result of this penny-pinching where money is needed the most can be seen in the disrepair of public facilities and institutions of all kinds--from schools and hospital buildings, to the electricity grid, to bridges and tunnels. Here, THOMAS BARTON, a New York City public hospital worker and shop steward in AFSCME District Council (DC) 37 Local 768, reports on the hazard at Bellevue.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">
<P>THE CRACK in Room CD421 in Bellevue Hospital's CD wing is getting wider. It runs from the floor to the ceiling, jagged, like a lightening bolt, and then along the ceiling. It's wide enough now to stick your hand in halfway to the second knuckle. </P>
<P>Hospital management says that there's nothing to worry about. In the parking lot, nine lifts for parking cars are closed off by yellow police tape. They were the first sign of trouble, back in July--when they began slumping toward the huge hole next to the CD wing where a new building is going in. Management said that there was nothing to worry about.</P>
<P>One nurse told me that she couldn't get into her room in the CD wing, and a hospital security officer standing nearby says security couldn't get into part of their area either. The doors to their rooms were jammed shut when the building shifted. Management says that there's nothing to worry about.</P>
<P>Last month, there was apparently another shift, breaking a water line at First Avenue and 26th Street, right next to the construction hole. One manager said that half a million gallons of water went into the hole on Saturday night and early Sunday morning, soaking and spoiling 30 truckloads of concrete that had been put in to help shore up the CD building.</P>
<P>Meanwhile, a social worker who has an office in the CD building recently asked if we "get the vibrations, too?"  </P>
<P>"Oh, a couple times a day we can feel it through our feet," she says. "The building kind of vibrates."</P>
<P>On the Monday after the water main break, a huge crack appeared on the outside of the CD building, running in a jagged line, from the top down to the second floor. That was the same weekend that the wall split open in Room CD421. The outside crack is widest at the top of the building--and a construction worker says, "You think that's bad, you ought to see the roof." </P>
<P>By the estimate of one informed source, the crack was caused because the top of the CD wing is leaning--by at least five inches--toward the giant construction hole. One possible reason: according to a worker in the facilities management office, the construction company dug their hole--now about three stories deep--six feet closer to the CD wing than plans allowed. </P>
<P>Shop stewards at Bellevue called their union to ask for something to be done. Unfortunately, all that DC 37 officials did is request a letter from the construction company engineers saying that the CD building is safe. Meanwhile, workers in the CD wing live in fear of what could happen next. Can anybody say for sure that the building is coming down? No. But can anybody say that it's safe? No way.</P>



