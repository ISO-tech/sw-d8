<P><font face="Times New Roman, Times, serif" size="5"><b>"Racial privacy" initiative is racism in disguise</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 26, 2003 | Page 4

<P><font face="Times New Roman, Times, serif" size="3"><B>Dear<I> Socialist Worker,</B></I><br>
Ward Connerly is at it again. Connerly is the conservative Black businessman on the University of California Board of Regents who spearheaded Proposition 209, the 1996 initiative that banned affirmative action in California public education, hiring and contracting.</P>
<P>Now, Connerly is pushing Proposition 54, the so-called "Racial Privacy Initiative" that would prevent the state from collecting almost all racial data. He claims that this will help make California a colorblind society. This is like saying that we can eliminate poverty if we stop collecting data on income and wealth.</P>
<P>At one recent public appearance, Connerly made the outrageous claim that there is little racial discrimination in education and employment. But recent studies have demonstrated the massive degree of racism that exists in the job market.</P>
<P>In one, conducted by a Northwestern University sociologist, two college students--one white, one black--with nearly identical resum&eacute;s, applied for jobs. The only difference was that the white applicant admitted to an 18-month prison term for drug dealing. Despite this, the white applicant was called back more often than the black applicant with no criminal record--17 percent to 14 percent of the time.</P>
<P>Another study found that applications from job seekers with white-sounding names were 50 percent more likely to get interviews than applicants with names (like Jamal and Lakisha) commonly identified with African Americans. The study concluded that a white-sounding name is worth as much as an additional eight years of job experience.</P>
<P>Proposition 54 will simply make it more difficult to document and combat discrimination. It is vitally important that as many Californians as possible vote against this racist initiative.</P>
<B><P>Phil Gasper</B>, Berkeley, Calif.</P>

