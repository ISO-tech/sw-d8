<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Obscene poverty in a world of plenty</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 26, 2003 | Page 3

<P><font face="Times New Roman, Times, serif" size="3">ECONOMIC EXPERTS are rejoicing that a global recovery is picking up steam. "We meet at a time of rising optimism," International Monetary Fund (IMF) Managing Director Horst Koehler told reports in Dubai prior to the IMF's annual conference this week.</P>
<P>But for billions of people around the world suffering in poverty, there's little reason for optimism or celebration. The crisis they face was highlighted earlier this month when 22 developing countries walked out of World Trade Organization (WTO) talks in Canc&uacute;n, Mexico. The breakdown of the WTO summit exposed the bullying of U.S. and European Union officials who refused to end government subsidies to agribusinesses unless poorer countries loosened rules on international investment to allow further domination by huge multinational corporations.</P>
<P>Rich countries portray their vast expenditures on these subsidies as a way to help "family farmers." But in reality, the $350 billion spent each year by industrialized nations on such subsidies--a sum greater than all of Africa's annual economic output--is nothing more than corporate welfare to insure the profitability of the world's largest food corporations. Meanwhile, as even World Bank President James Wolfensohn admits, the subsidies are "crippling Africa's chance to export its way out of poverty." </P>
<P>The World Bank and the IMF promote themselves as institutions dedicated to alleviating poverty. But their own prescriptions have drowned poor countries in debt--and the rich countries that call the shots pursue policies that only make matters worse for the world's poor. Trade barriers erected by the world's richest countries cost the developing world $200 billion a year--twice as much as they receive in aid. </P>
<P>Paying lip service to ending poverty isn't a skill practiced only by international economic institutions. George W. Bush claims that his "compassionate conservatism" will help poor people in the U.S. Tell that to the 1.3 million Americans who dropped below the poverty line last year alone.</P>
<P>Today, the 500 richest people on earth have a combined net worth of $1.54 trillion. That's greater than the combined annual incomes of the world's 3 billion poorest people.</P>
<P>The vast poverty that exists amid plenty in today's world is completely unnecessary. But for the super-rich, there's a method to the madness. Their wealth depends on people around the world suffering poverty and desperation--while billions of others work harder and longer then ever, but continue to see their living standards decline in a "race to the bottom."</P>
<P>A socialist society--where workers collectively decide on what to produce and how to distribute it--would eradicate poverty and free humanity from the dehumanizing conditions of work organized around the corporate bottom line. That's a world worth fighting for--beginning in the here and now.</P>

