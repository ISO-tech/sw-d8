<P><font face="Times New Roman, Times, serif" size="4">Deadly attacks on Palestinians</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Israel's rampage</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 20, 2003 | Page 12</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WITH A series of deadly Israeli rocket attacks on Palestinian neighborhoods in Gaza, Israeli Prime Minister Ariel Sharon dramatically tore up the U.S.-backed "road map" to Middle East peace. Just days after Sharon met with George W. Bush and Palestinian Prime Minister Mahmoud Abbas--at a summit hailed as a "historic" breakthrough--Sharon's government launched five helicopter raids in Gaza in the space of 48 hours, targeting leaders of the Islamist resistance group Hamas.</P>
<P>As usual, the U.S. media--after ignoring the anti-Palestinian slant of the road map during the summit--focused on Hamas' revenge attack, a suicide bomb set off in a Jerusalem bus that killed 17 people. But the suicide attack was the entirely predictable response to a clear Israeli provocation--the attempt to assassinate Hamas political leader Abdul Aziz al-Rantissi.</P>
<P>Rantissi survived--though three other Palestinians, including a baby girl, were killed. All told, Israeli attacks had killed at least 23 Palestinians as of last weekend. </P>
<P>In one of the raids, Yasser Taha, a leader of Hamas' military wing, was incinerated in his car--along with his wife and his 2- and 3-year-old children. Another missile fired at Taha locked onto a nearby car, killing its three passengers.</P>
<P>At least 40 more people were injured, many of them mourners from a huge funeral procession for 11 Palestinians killed in earlier strikes. As more people arrived on the scene, another missile struck, injuring those who came to help. "The car was completely smashed," said Samir Akram, who rushed to the burning heap of steel and rubber to help. "Flesh and blood was mixed with the metal. We barely could pull out the body parts."</P>
<P>Immediately after returning to Israel from the summit in Aqaba, Jordan, Sharon showed his eagerness to torpedo Bush's "peace" deal, by approving the assassination of Rantissi--the highest-level Palestinian official targeted by Israel since the beginning of the new Palestinian uprising, or Intifada, 33 months ago. Sharon's blatant disregard for the guidelines of the road map he supposedly accepted stood exposed.</P>
<P>Even Bush--who pushed the road map on Palestinians and other Arab leaders during his Middle East visit precisely because it will help Israel tighten its hold--had to declare that he was "deeply troubled" by the attack on Rantissi. But Sharon's provocation accomplished its goal--triggering a response by Hamas.</P>
<P>The Bush administration quickly retreated from even its mild criticism of Sharon and lashed out at the Palestinians. "White House spokesman Ari Fleischer put the full blame for this week's setbacks to the peace plan on Hamas, which staged an attack Sunday in coordination with two other militant groups that killed four Israeli soldiers and started the tit-for-tat violence," wrote the <I>Detroit Free Press.</P>
</I><P>What an obscene distortion! Every day since the Aqaba summit--even the day <I>of </I>the summit--Israeli soldiers have carried out sweeps of Palestinian towns, maiming and killing dozens. Fleischer is blaming Palestinians for starting the violence, when, in truth, Israel's violence never stopped.</P>
<P>The White House wants to pin the label "terrorism" on any act of Palestinian resistance--even when the target is Israeli soldiers in Palestinian territory. But it won't condemn Israel's relentless killing of Palestinians. It's as if killing people is fine as long as the killers wear uniforms, fly American-made helicopters and fire American-made rockets.</P>
<P>The same double standard applies to Washington's support for the road map--something that wasn't lost on most Palestinians, who have a legitimate skepticism about U.S.-backed "peace" plans that have only led to intensified misery under Israeli occupation. In the words of Palestinian author Edward Said, the "road map...is not about a plan for peace so much as a plan for pacification. It is about putting an end to Palestine as a problem...based on the notion that the underlying problem has been the ferocity of Palestinian resistance, rather than the occupation that has given rise to it."</P>

