<font face="Times New Roman, Times, serif" size="5"><b>Walkout over wage cuts at Chicago hotel</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lauren Fleer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 20, 2003 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"THE CONGRESS is cheap, cheap and mean!" That slogan rang out from the crowd of housekeepers, restaurant employees and bellmen as they walked off the job at the Congress Hotel in downtown Chicago last weekend. </P>
<P>About 135 members of Hotel Employees and Restaurant Employees (HERE) Local 1, whose contract expired nearly six months ago, went out on strike when management unilaterally cut wages and benefits. Four were arrested on the first day of the walkout for refusing to get off the city sidewalk under the hotel's front awning.  </P>
<P>In mid-May, the Congress cut wages by 7 percent and effectively eliminated health care and pension benefits by refusing to pay the monthly premiums. Edgar Ocana, who works as a waiter at the Congress, had his hourly wage of $5.25 slashed to a mere $4.77 an hour when the cuts were implemented. "The Congress sells expensive food--why are they so cheap when it comes to us?" he asked.</P>
<P>Negotiations broke down in May when the bosses declared an impasse. Workers are demanding that the Congress comply with the citywide standard established last September, when 7,000 other HERE members at 28 Chicago hotels won an hourly wage of $10, with a 37 percent increase over four years.  </P>
<P>Instead of offering to raise wages that already were among the lowest in the nation, Congress management stated that it was "unwilling" to offer anything. Last month, it implemented its pathetic and insulting final offer--a 7 percent pay cut, the right to subcontract out any and all jobs and a refusal to pay health care premiums at the level required by the provider, effectively eliminating health care coverage. Workers responded with a 113-1 vote authorizing a strike. </P>
<P>"We've got families to feed and bills to pay," said restaurant cashier Tina Lee. "They aren't even looking at that. Dean Rubin, the general manager, gets to stay in a company house while people like me actually have rent they're trying to pay."</P>
<P>The Congress Hotel has threatened to close its doors several times, and management justifies the cuts by citing its alleged financial distress. "If they do shut down, so be it," says HERE President Henry Tamarin. "If the plantation can't survive without paying slave wages, it's time to board up the big house. Let there be no more wage slavery in Chicago."</P>
<P>Despite the Congress' threats to shut down, the hotel hired replacement workers from Labor Temps to stay in business. While front desk, clerical and security employees are still at work, strikers are maintaining a 24-hour picket line outside and encouraging guests to take their business elsewhere. "This will be a citywide fight," promised HERE organizer Dan Miller. "We're going forward, not back. We'll be out here for as long as it takes."</P>


