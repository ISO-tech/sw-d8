<P><font face="Times New Roman, Times, serif" size="4">Immigrant killed in workplace accident</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Horror at a Texas factory</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Cindy Beringer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 20, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">DANIEL CRUZ ROMERO was ground to pieces June 13 when he was caught in a meat processing machine at a factory north of Austin, Texas. The 34-year-old was only the latest among a group of Mexican immigrants to meet death in Texas in ever more gruesome ways.</P>
<P>Romero had complained to his housemate and former co-worker about working conditions at the frozen food manufacturer, Michael Angelo's Gourmet Foods, where he had worked for three years. In particular, Romero said that the machine that he worked on started up when the safety lid was open. </P>
<P> Michael Angelo's had been cited several times in the previous three years by the Occupational Safety and Health Administration (OSHA), for more than 50 safety violations that could have resulted in serious injury or death. Five of the violations were deemed "willful."</P>
<P>In a February 2001 news release, the Austin director of OSHA said, "Investigators found machine safety interlocks deliberately bypassed in order to speed up production and maintenance work on machinery that was not properly locked out of service, putting employees at risk for amputations and other serious injuries...Preventable injuries were occurring in the plant, and the employer did not take prompt corrective action to reduce these injuries."</P>
<P>Management at Michael Angelo's should be found guilty of murder. But chances are that they'll get off with a fine--if that.</P>
<P>For immigrants like Romero--who every two weeks sent a part of his paycheck back home to Mexico to take care of his 5-year-old daughter and 9-year-old son--there is often little choice but to take low-paying jobs in dangerous conditions. But this is only one way that immigrants suffer in Bush's America.</P>
<P>On the same day that Romero was killed, immigrant rights supporters in Harlingen, Texas, along the Gulf Coast, protested the shooting death of another Mexican immigrant in February. Juan Patricio Peraza was stopped by Border Patrol agents while taking out trash at a temporary shelter for undocumented workers. </P>
<P>The 19-year-old was shot by when he ran and allegedly threatened the officers with a metal pipe. But witnesses to the killing said Peraza wasn't a threat. And the record of the Border Patrol is so terrifying that even children who are U.S. citizens often run when they see agents.</P>
<P>When the grand jury failed to indict the agent that killed Peraza, a human rights group marched to demand a federal inquiry into the case. This abuse of immigrants in Texas must stop!</P>


