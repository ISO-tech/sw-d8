<P><font face="Times New Roman, Times, serif" size="5"><b>Hoffa deal won't save jobs in carhaul</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Donny Schraffenberger, Steward, Teamsters Local 705</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 20, 2003 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">TEAMSTER CARHAUL workers, who include drivers, dockworkers, office workers and mechanics, will vote on a tentative contract at the end of this month. The five-year deal will have no hourly wage increases for the first two years of the deal, and only 40 cents, 40 cents and 50 cents in the remaining three years.</P>
<P>The 1999 contract was a four-year deal that failed to take advantage of the auto industries' profitable years. Since that deal, Teamster carhaul workers have lost jobs. The union represented more than 12,800 carhaulers in 1999; today it's only 9,500.</P>
<P>Overall, in 2002, the Teamsters declined by 48,000 members. Teamster Organizing Director Jeff Farmer estimates that as many as 200,000 Teamsters might be gone by 2006.</P>
<P>Clearly, drastic action is required. Instead, longer contracts that don't even preserve the status quo are clearly eating away at the strength of the Teamsters.</P>
<P>On June 10, Teamster carhaul local leaders unanimously voted to approve and recommend the tentative contract to the membership. "This is a solid contract, one that preserves our strong health, welfare and pension benefits and provides new language to protect and create jobs, which is vital to our future in this industry," said Teamster President Jimmy Hoffa. Yet, Hoffa claimed that the 1999 deal was "historic" and that "the Teamsters are back," only to witness the steady erosion of carhaul members.</P>
<P>The 2003 contract has no improvement in holidays, sick days and funeral leave. Also, Allied Automotive Group, the largest carhaul employer, rejoined the carhaul employers' association and signed on to the master agreement at the last minute. Previously, Allied was bargaining separately with the Teamsters.</P>
<P>Clearly, in unsteady economic times as these, the carhaul bosses will be doing their utmost to destroy good-paying union jobs. Sadly, the Teamster leadership has no plan to stop the bosses' attack.</P>
<P>Even though Hoffa promises that this contract will protect and create jobs, as the last contract proved, this is very wishful, if not delusional, thinking. If the last four years are any indication, Teamster carhaul workers are in trouble, unless our union becomes more aggressive--starting from the bottom up. </P>


