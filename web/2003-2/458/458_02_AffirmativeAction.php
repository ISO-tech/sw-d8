<P><font face="Times New Roman, Times, serif" size="4">Supreme Court lawsuits mark the latest stage in...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The assault on affirmative action</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 20, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE U.S. Supreme Court was due to issue its decision on two cases that could decide the future of affirmative action as <I>Socialist Worker </I>went to press. In the two lawsuits, white defendants charge that the admissions process at the University of Michigan Law School and the university's undergraduate school supposedly unfairly favored Black and Latino students over whites.</P>
<P>Three white applicants who were rejected by the law school and undergraduate program schools in the 1990s argue that they were denied their constitutional right to "equal protection." They claim that they suffered "reverse discrimination"--because African American and Latino students with similar or lesser academic records were admitted instead of them.</P>
<P>While the three students' names are on the court cases, the real moving force behind this attack on affirmative action is much bigger. A group called the Center for Individual Rights solicited plaintiffs with a case against the Michigan affirmative action plans.</P>
<P>The Center is a collection of right-wing bigots. In its early years, it received financial support from the Pioneer Fund--an organization that spent decades trying to push the idea that whites are genetically superior to Blacks.</P>
<P>If the Supreme Court rules against the University of Michigan's policies--in either one of the two cases--it will be a victory for opponents of affirmative action, who will only grow more emboldened to go after other programs. And it will be a victory for the Bush administration, which threw its support behind the attack on affirmative action--announcing, on the Martin Luther King holiday, that it was filing a friend-of-court brief to back the two lawsuits' claim that Michigan's policies were "unconstitutional." Bush assigned Solicitor General Ted Olson, who worked for the Center for Individual Rights before he worked for Bush, to file the brief.</P>
<P>Affirmative action, like school desegregation, was a reform won because the civil rights movement forced the federal government to recognize the systematic and institutional racism that thrived in U.S. schools. Segregation is far from being eliminated in America's schools--and this assault on affirmative action programs will only make matters worse.</P>
<P>Take the University of Georgia, where a federal appeals court barred the use of affirmative action two years ago. Black enrollment has fallen from 6.2 percent in 1988 to 5.5 percent last fall--in a state that is 29 percent Black.</P>
<P>On April 1, some 50,000 affirmative action supporters turned out for a demonstration in front of the Supreme Court. And actions are scheduled in several cities when the Supreme Court announces its decision. We have to speak out against this attack--and stop the right from turning back the clock on affirmative action. </P>


