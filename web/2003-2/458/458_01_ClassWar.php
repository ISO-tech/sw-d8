<P><font face="Times New Roman, Times, serif" size="4">Robin Hood in reverse</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bush's class war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 20, 2003 | Page 1</font></p>

<P><font face="Times New Roman, Times, serif" size="3">THE BUSH administration has declared war on poor and working class people in the U.S. Their budget proposals slash away at every social program that working people need--from housing to Head Start to Medicare.</P>
<P>Why? To pay for the most massive tax cut giveaway to the rich in decades. This is Robin Hood in reverse--stealing from the poor to line the pockets of the rich.</P>
<P>Just to underline the point, the version of Bush's tax cut that became law did away with a $400 child tax credit for 6.5 million low-income families. Republican leaders in Congress said they couldn't "afford" the $3.5 billion to pay for this.</P>
<P>But they did manage to find enough for the "Hummer deduction"--which allows business owners to deduct up to $100,000 for the cost of a vehicle, as long as it weighs at least 6,000 pounds. Now Congress is deadlocked over proposals to restore the child tax credit--with lunatic House Majority Leader Tom DeLay (R-Texas) insisting on adding even more giveaways to the filthy rich.</P>
<P>Democrats are making a fuss about the child credit. But they've caved again and again to the White House. As arch-conservative Republican insider Grover Norquist gleefully told a reporter, "In each of these fronts, we've found that we've pushed, and there's an open door. We'll keep moving forward as fast as we can. "No one I have talked to has any expectation that we will have anything less than a tax cut every year of the eight years of the Bush administration."</P>
<P>Next up for the White House con artists is the new Medicare prescription drug plan. What it's really a prescription for is ripping off more seniors.</P>
<P>Democrats are patting themselves on the back for winning a "compromise" that stopped the Bush administration from providing drug coverage only to seniors who go into an HMO plan. But so many seniors will be unable to afford the "compromise" plan that analysts say it will probably accomplish the administration's goal of pushing push <I>more</I> people into HMOs.</P>
<P>Because of the tax cut giveaways to the rich, the federal government budget deficit is projected to jump to more than $400 billion. But that's part of the Bush gang's long-term project.</P>
<P>They <I>want </I>to create a fiscal nightmare--so that for decades to come, politicians can plead poverty every time there is a demand for increased spending on the programs that working people desperately need. This is class war. Our side needs to organize and fight back.</P>


