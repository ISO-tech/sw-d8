<P><font face="Times New Roman, Times, serif" size="5"><b>Why did Charleston ILA cross a picket line?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 20, 2003 | Page 10</font></P>

<P><font face="Times New Roman, Times, serif" size="3">PICKET LINES by East Coast dockworkers forced shipper Evergreen America Corp. to withdraw charges that the union was conducting an illegal strike. The dispute began May 14 when Taiwan-based Evergreen, one of the world's biggest container carriers, refused to accept a 3-2 vote by port captains in Port Elizabeth, N.J. to join the International Longshoremen's Association (ILA). </P>
<P>Although the National Labor Relations Board (NLRB) certified the vote as valid, Evergreen refused to recognize the ILA and asked a federal court to intervene. In response, the ILA set up picket lines up and down the East Coast, causing massive backups for Evergreen on the eve of peak season.</P>
<P>Management retaliated by seeking to have the picket lines ruled an illegal secondary boycott under the anti-union Taft-Hartley law. But on June 10, the effectiveness of the picket lines finally forced Evergreen to withdraw that charge and return to the bargaining table.</P>
<P>The main exception to this solidarity came in Charleston, S.C., where some members of ILA Local 1422 crossed a picket line to work an Evergreen ship. "Our commitment to solidarity is as solid as ever, but this one is sort of different," Kenneth Riley, president of International Longshoremen's Association Local 1422, told the <I>Charleston Post and Courier</I>.</P>
<P>Many longshore workers and labor activists were rightly shocked that Local 1422 members crossed a picket line. The local includes four of the Charleston Five--dockworkers who faced nearly two years of house arrest and the threat of jail time on trumped up charges following a police attack on their picket line in January 2000.</P>
<P>Riley traveled across the country and internationally building solidarity for the five--and dockworkers around the world pledged to take action if the case went to trial. The struggle ended in victory because it embodied the slogan of labor solidarity, "an injury to one is an injury to all."</P>
<P>The port captains in New Jersey deserve that solidarity, too. Riley was unavailable for comment as <I>Socialist Worker </I>went to press. </P>

