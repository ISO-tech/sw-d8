<P><font face="Times New Roman, Times, serif" size="4"><i>Letters to a Young Activist</i> from Todd Gitlin</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Return to sender</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Todd Chretien</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 20, 2003 | Page 9</font></P>
<P><font face="Arial, Helvetica, sans-serif" size="2">Todd Gitlin, <B><I>Letters to a Young Activist,</B></I> Basic Books, 2003, 174 pages, $22.50.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IMAGINE A "young activist" who picks up Todd Gitlin's new book, seeking useful advice from the former president of the premiere antiwar student organization of the 1960s, Students for a Democratic Society. They will be surprised to find that history's two most dangerous people are not members of Bush's cabinet, but rather the Russian revolutionary Lenin and 2000 Green Party presidential candidate Ralph Nader. (Noam Chomsky comes in a distant third).</P>
<P>Running throughout the condescending prose and sloppy fact-checking in Gitlin's<I> Letter to a Young Activist</I> lies the assertion that any challenge to the Democratic Party is either the product of youthful folly or crazed sectarianism. His central historical contention about the 1960s movements is that everything was going fine until people starting thinking that real social justice required some type of revolution or looked to Lenin--who he describes as an "intellectually dishonest" monster--for ideas.</P>
<P>For instance, Gitlin claims that "the Black Panther Party hijacked the black liberation movement" and slanders the urban Black rebellions that broke out after Martin Luther King Jr.'s assassination as "the momentary exultation of looters, arsonists and snipers." Gitlin also harbors a rather ambiguous attitude to the McCarthyite anti-Communist witch trials, which he calls a "mixed blessing."</P>
<P>Having smeared any attempt to challenge U.S. capitalism at its roots, Gitlin staggers on into the realm of the absurd. "If antiwar militancy can take credit for driving Johnson from office in 1968," writes Gitlin, "it must...shoulder the blame for nudging some voters towards Nixon, who proceeded to extend the Vietnam War for five years and expanded it to Laos and Cambodia, killing more than a million people."</P>
<P>Playing fast and loose with the facts, Gitlin tells his young activist reader--who he prefers to call a "social entrepreneur"--that had the antiwar movement supported Democrat Hubert Humphrey (who personally helped escalate the war for the five previous years as Johnson's vice president) for president in 1968, "he would have phased out the war." Thus, the lesson is, if you don't vote for the Democrats, you are morally responsible for Nixon and Pol Pot.</P>
<P>After a chapter claiming that racism is fading away and Black students only "proclaim their marginal identity" as a way of filling an existential void, Gitlin attacks activists organizing for justice for Palestinians against Israeli apartheid. In other chapters, he assails anyone who won't salute the American flag, chastises "feminist zealots [who] were sometimes indifferent to children" and calls for the left to join in the war on "Islamic murderers."</P>
<P>But the real point of Gitlin's whole enterprise is to draw a straight line from his version of 1960s history to today: A vote for Nader or any third-party candidate was (and would be again) a vote for Bush because "you either vote Democratic, or submit to the rule of the Republicans. "The Democratic Party is the inescapable field where we either win, lose or draw." </P>
<P>Gitlin advises young activists not to be intimidated by their political opponents--but this is precisely what his book aims to do. He contemptuously dismisses any attempt to build a genuine radical political party as "narcissism wearing a cloak of ideals."</P>
<P>Give this book a read as a brief to prepare for the liberal Democratic war against anyone who dares to challenge the "lesser evil in 2004" reasoning. But at $22.50, Gitlin's 161 pages aren't worth it. Steal this book.</P>

