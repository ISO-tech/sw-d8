<p><a name="RedPill"></a><font face="Times New Roman, Times, serif" size="4"><b>You missed the point in the <I>Matrix</b></font></P>
</I>
<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</B>,</P>
</I><P><font face="Times New Roman, Times, serif" size="3">Your review of <I>The Matrix: Reloaded </I>(May 6) was disappointing. For one thing, it fails to acknowledge that this is the second film in a trilogy. Squeezed between the opening film and a third part that will presumably conclude the tale, <I>Reloaded</I> necessarily raises more questions than it answers. Hadas and Laura suggest that <I>Reloaded</I> falls short because it "misses the opportunity to explore resistance and collective struggle among the free human population of Zion." </P>
<P>The review quotes a character from the first <I>Matrix</I> who says that when the revolution comes, "Zion's where the party would be." Taking this as their cue, Hadas and Laura are disappointed that <I>Reloaded</I> doesn't offer up the promised revolutionary party (pun intended). But perhaps they were as misled as Tank, the character they quote. </P>
<P>The defining moment in that film was not captured in Tank's fantasy, but in the training program where Morpheus tells Neo that "if you are not one of us, you are one of them." The film uses this as its starting point--the idea that the masses of ordinary people are duped by the system they live in. It's an approach to reality that comes straight out of academically fashionable theories of "postmodernism."</P>
<P>In this sense, the Matrix films are actually based on a much more elitist concept of the world than the reviewers acknowledge. To take literally the promise of collective resistance and to search for it in <I>Reloaded--</I>then fault the film for not providing it--is to miss the point of the film. </P>
<P>I was also surprised that the review failed to mention the primitivist, racist way in which the residents of Zion are represented. At one point, Neo opens a door to find a crowd of people begging him to save them. They are all, predictably, "third-world" characters, who then leave offerings outside his room--it's the white messiah acting as the savior of desperate third-world masses. How does this sit with a film that employed well-known leftist cultural critic Cornell West as one of its advisors and actors?</P>
<I><P>Reloaded</I> raises questions about free will, fate, the role of technology, the relationship between religion and resistance, and a whole lot more. But the review glosses over all of this to focus on what the reviewers are interested in: revolution. </P>
<P>Will the third installment meet our reviewers' criteria? Maybe. After all, it is titled <I>Revolutions</I>. But don't count on it.</P>

<B><P><font face="Times New Roman, Times, serif" size="3">Ganesh Lal, </B>Greensboro, N.C.</P>


