<P><p><font face="Times New Roman, Times, serif" size="4"><b>We can't take four more years of Bush</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</B>,</I> </P>
<P><font face="Times New Roman, Times, serif" size="3">Our fragile world cannot tolerate another four years of Bush, Cheney, Ashcroft, Perle, Rove and Rumsfeld. While I wholeheartedly agree with your sentiment that our country would benefit from less corporate greed and more attention to the average person, I have a differing view on how to fix the problem.</P>
<P>You need to fight for the cause in your words and your activism, bring hearts and minds over to the left, but recognize the political reality: A socialist candidate today has no chance of winning nationwide office. </P>
<P>Every vote that is not cast for the Democratic challenger in the 2004 election is a vote for Bush. There are times when the Democrats and Republicans offer too similar a choice. Now is not one of those times.</P>
<P>Elect a moderate or elect a right-wing extremist: the choice may be yours. Or do you not remember Florida?</P>

<B><P><font face="Times New Roman, Times, serif" size="3">Donald Williams, M.D., </B>Austin, Texas</P>

