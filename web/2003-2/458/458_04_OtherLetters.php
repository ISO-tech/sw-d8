<p><a name="Bash"></a><font face="Times New Roman, Times, serif" size="4"><b>Out of the closets and into the streets</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</B>,</I> </P>
<P><font face="Times New Roman, Times, serif" size="3">Jessica (legally known as Hector Mercado), a 24-year-old transvestite, was found stabbed to death in her New Haven, Conn., apartment on May 9. Her body had been set on fire in an apparent attempt to disguise the cause of her death. </P>
<P>Jessica worked as a prostitute; it is likely that her assailant was surprised by her gender and murdered her for that reason. Yet police have refused to investigate the murder as a potential hate crime.</P>
<P>Violence against gays, lesbians, bisexuals and transgendered (GLBT) people is, of course, nothing new. Thirty-four years ago this month, GLBT people and their allies famously held off a New York City Police riot squad for three days following cop assaults on lesbians and transvestites at the Stonewall Inn.</P>
<P>In the months to come, hundreds emerged from the closet and into the streets, fighting back in the newly organized Gay Liberation Front and other organizations. Black radical Huey Newton sent letters of support from prison, also calling for an end to homophobia within the Black Panthers. The Young Lords, a group of Puerto Rican radicals, planned joint actions in the streets of New York City. </P>
<P>In these times of physical assault against GLBTs and Arabs, and murders of unarmed African Americans by the NYPD, we must fight back in local struggles. As the Bush administration takes away affirmative action and women's right to abortion and ensures that GLBT people do not enjoy full civil rights, more than ever, we have to fight for the liberation of all working-class people. </P>

<B><P><font face="Times New Roman, Times, serif" size="3">Phil Haskell, </B>New Haven, Conn.</P>

