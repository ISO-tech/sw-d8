<P><font face="Times New Roman, Times, serif" size="4">Mayor demands "sacrifice"</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Taking aim at New York's poor</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>by Aaron Hess</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 20, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"THIS WILL be painful," said New York City Mayor Michael Bloomberg at a press conference in early June. "But we all have to understand that we have to make sacrifices."</P>
<P>In fact, "we all" won't be sacrificing equally. Bloomberg is determined to balance the city's budget, which is $3.8 billion in the red--and he plans to do it almost entirely on the backs of poor and working New Yorkers.</P>
<P>Bloomberg, the 63rd richest person on the planet, could wipe out the entire city deficit himself if he cashed in his personal fortune--and he'd still have more than $1 billion left over. Instead, he's taking aim at the city's most vulnerable. </P>
<P>Kids are first in line. For example, the Summer Youth Employment Program, a city service that provides work experience for inner-city teens during the summer months, is facing a 90 percent cut. Summer school programs will be slashed by $33 million. Twelve children's health clinics are scheduled to be shut down. The city is "cutting nuts and bolts...and the heart and soul of city life for children," Rose Anello, associate executive director of the Citizens' Committee for Children of New York, told <I>Newsday.</P>
</I><P>More than 3,000 city workers--including teachers' aides, sanitation workers and computer technicians--have been laid off in the last month alone. And more pink slips are on the way. Officially, unemployment in New York is already at 8.6 percent--almost half again the national average.</P>
<P>New York isn't alone in facing a massive fiscal crisis. All told, state and local governments around the country face a total budget shortfall of nearly $200 billion. Politicians blame increased spending on social services. But this is wrong. The real source of the budget crisis is the massive tax cuts handed out mainly to corporations and the rich during the 1990s boom--with New York's former Mayor Rudolph Giuliani leading the way.</P>
<P>Groups of New Yorkers are fighting back. In the Williamsburg neighborhood of Brooklyn, community activists are fighting to stop the city from closing Engine Company 212. Nicknamed the "People's Firehouse" in 1975 after hundreds occupied it in a successful struggle to keep it open, residents are determined to keep 212 from being closed again. "Our safety is too important to be sacrificed," said Wendy Rivera, who works with an ad-hoc coalition called the Community Action Group. She and other residents have kept up a 24-hour vigil at the firehouse for several weeks.</P>
<P>Recent polls show massive bitterness toward Bloomberg and his priorities. According to the <I>New York Times, </I>Bloomberg's approval rating has plummeted to 24 percent--the lowest recorded by any New York mayor in decades. Daily stories in the media about the harsh realities of Bloomberg's rule--such as a front-page <I>New York Daily News </I>article about police issuing a summons to a pregnant woman for sitting on the steps of a subway station--have helped fuel the outrage.</P>
<P>Recently, Bloomberg was even forced to restore $90 million in cuts--a sign that popular pressure can make the budget-cutters in City Hall back down. We have to send a message: ordinary New Yorkers shouldn't be forced to sacrifice for a crisis that we didn't cause. We need to defend our services from billionaires like Michael Bloomberg--and build an alternative that fights to tax the rich and put our priorities first.</P>


