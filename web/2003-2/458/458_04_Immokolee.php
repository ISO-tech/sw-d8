<P><font face="Times New Roman, Times, serif" size="4">YUM! treats chickens better than farmworkers</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Modern-day slavery</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 20, 2003 | Page 4</font></p>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</B>,</I> </P>

<P><font face="Times New Roman, Times, serif" size="3">YUM! Brands--which owns Taco Bell, KFC, Pizza Hut, Long John Silver's and A&amp;W All American Food Restaurants--apparently thinks that chickens are more important than human beings. That's the only conclusion one can draw from a recent agreement the corporation made with People for the Ethical Treatment of Animals (PETA). </P>
<P>Kentucky Fried Chicken has promised PETA that it will increase living space for its chickens and make sure that the animals are not abused. In a statement, YUM! said that it will "only deal with suppliers who maintain the very highest standards and share our commitment to animal welfare."</P>
<P> YUM! Brands doesn't have any commitment to human welfare, however. Farm workers in Immokalee, Fla., who pick tomatoes for Taco Bell are paid just 40 to 50 cents for a 32-pound bucket. A single worker has to pick two tons of tomatoes just to make $50. The workers have no benefits, no overtime pay, no sick leave, no vacation pay and no medical insurance and are denied the right to organize. </P>
<P>Immokalee is "ground zero for modern slavery," according to <I>New Yorker</I> reporter Jim Bowes. "We have investigated cases where people have been pistol-whipped, held at gunpoint, beaten and told they would have their tongues cut out if they talked to the authorities," says Lucas Benitez, of the Coalition of Immokalee Workers. "Of course, that's the extreme of exploitation in the fields, but sweatshop conditions--sub-poverty wages, no right to organize, no right to overtime pay, no health insurance, no benefits at all--are our everyday reality. </P>
<P>On May 15, YUM! Brands had a shareholders' meeting at its corporate headquarters in Louisville, Ky. Outside, about 100 farmworkers and their supporters gathered to protest. They demanded that Immokalee workers be paid a penny a pound more for the tomatoes they pick. </P>
<P>Some of the protesters dressed as farm animals to point up the company's hypocrisy. As Edvin Lopez, a migrant worker from Guatemala, told the <I>Lexington Herald Leader</I>, "YUM! has said they have a responsibility to animals. We are asking them to have the same respect for us as human beings and workers."</P>
<P>Antonio Martinez, who says he was held as a slave laborer for a time at a farm near Naples, Fla., called for all Hispanic migrants to join the movement. "This is a struggle for all of us migrant workers from Mexico, not just those who pick tomatoes," he said.</P>
<P>Inside, David Novak, the CEO of YUM! Brands, boasted that his company had enjoyed at least 15 percent growth in international operating profits, and that revenues had surged by 12 percent. And, of course, their chickens are being treated nicer.</P>

<B><P><font face="Times New Roman, Times, serif" size="3">Evan Kornfeld, </B>Los Angeles</P>

