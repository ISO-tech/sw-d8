<P><font face="Times New Roman, Times, serif" size="5"><b>Corporate vultures who prey on the sick</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 20, 2003 | Page 4</font></p>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</B>,</I> </P>
<P><font face="Times New Roman, Times, serif" size="3">In response to increasing pressure from New Haven County residents, Yale New Haven Hospital (YNHH) has been forced to forgive some patients' debts and stop foreclosures. Thousands of Connecticut families have faced enormous debts, lawsuits, interest payments, wage garnishments, bank executions, liens, foreclosures and bankruptcies--simply because they didn't have sufficient health insurance to afford care at YNHH. </P>
<P>New Haven's Hospital Debt Justice Project, headed by the Connecticut Center for a New Economy (CCNE) and local churches, provides appalling information about Yale's treatment of its poor and working-class patients. In 2001 alone, YNHH placed 134 new liens on the homes of New Haven patients. </P>
<P>In an editorial to the <I>New Haven Register,</I> CCNE researcher Grace Rollins points out that according to the Office of Health Care Access, Yale claims to have spent $2.9 million on "free health care" in 2001. In reality, Yale spent a mere $12,000 of that sum treating patients. The rest paid for private- and public-sector subsidies. </P>
<P>This is an outrage--especially considering the hospital's yearly collection of millions of dollars in Connecticut residents' tax money, supposedly for free care, Medicaid shortfall and bad debts! What's more, the institution behind the hospital, Yale University--one of the world's wealthiest universities and the country's third-richest nonprofit organization--is exempt from paying taxes to New Haven, the country's fourth poorest city. </P>
<P>That's why the Hospital Debt Justice Project is demanding an end to aggressive debt collection tactics, a thorough public review of the hospital's policies, the forgiving of unjust debts, and the implementation of policies to provide free health care to all those who are unable to pay. On June 7, Reverend Jesse Jackson visited the hospital to support the patients' cause, drawing more than 500 demonstrators to a spirited march.</P>
<P>The most impressive aspect of this struggle against debt-collection, however, is the fact that the battle is being waged alongside the drive for Yale-New Haven Hospital workers to unionize. In addition to their contract and unionization battles and their fight for fair housing and taxation of Yale, the Federation of Union Employees--which includes HERE Locals 34 and 35, GESO workers, and District 1199 workers--has rallied behind the Debt Justice Project. New Haven residents must build on their victory, and we must support their struggle for justice.</P>

<B><P><font face="Times New Roman, Times, serif" size="3">Meredith Kalman, </B>New Haven, Conn.</P>


