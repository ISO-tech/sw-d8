<P><font face="Times New Roman, Times, serif" size="5"><b>New protests in Iran sign of political crisis</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 20, 2003 | Page 5</font></P>

<P><font face="Times New Roman, Times, serif" size="3">STUDENT PROTESTS in Tehran and other cities are highlighting the rising social tensions in Iran, even as the U.S. steps up pressure on the country. George W. Bush called the demonstrations the "beginnings of people expressing themselves toward a free Iran," and U.S. officials criticized violent attacks on the protestors by vigilantes.</P>
<P>That gave Iran's hard-line religious conservatives an excuse to denounce the students as tools of Washington. The truth is much more complex.</P>
<P>The protests began June 9 when about 800 students demonstrated against the Islamist government's plans to privatize the universities--including the imposition of tuition fees. Although police arrested 80 protesters, another demonstration took place the following night--this time attracting some 3,000 people and taking up broader political demands.</P>
<P>Many joined in the chanting for the death of Ayatollah Sayed Ali Khamanei--the conservative cleric who is Iran's supreme leader--despite the risk of lengthy imprisonment for doing so. Khamanei has in recent months backed religious hard-liners opposed to reforms pushed by his rival, President Mohammed Khatami.</P>
<P>Khatami was elected in 1997 and re-elected in 2001 on a program of greater political openness and a relaxation of laws imposing religious behavior. However, religious councils controlled by the conservatives have blocked a series of measures that would have given Khatami greater power. Nevertheless, many protesters are frustrated by Khatami's refusal to mount a more aggressive challenge to the hard-liners, and called for his resignation.</P>
<P>After several days of protests, religious vigilantes (known as basij), armed with chains, knives and guns, launched a violent attack on demonstrators, beating students and raiding dormitories in the middle of the night. The clash--along with the spread of protests to the cities of Shiraz and Isfahan--raised the possibility of a repeat of the far larger student revolt of 1999, which vigilantes and police brutally put down.</P>
<P>As <I>Socialist Worker </I>went to press, the rival reform and conservative factions in Iran appeared to be trying to contain the situation. Reform newspapers allied with Khatami urged students to show restraint, while police arrested about 30 members of the basij for "hooliganism"--a setback for the right.</P>
<P>If Khamanei and Khatami are trying to close ranks to quell the student demonstrations, it's because they share an interest in preventing their spread to the working class. In recent years, strikes and worker protests have become commonplace, usually over unpaid wages. According to one estimate by an exiled Iranian group, some 100,000 workers have gone without pay for between three and 36 months.</P>
<P>In one small textile factory in the northern town of Behshahr, workers recently occupied their factory and went on hunger strike to win back pay. And after government officials banned May Day demonstrations, some 2,000 workers staged a peaceful sit-in at the state-controlled Workers House in Tehran to demand payment of back wages and a halt to privatization of state-run industries.</P>
<P>Such demands are completely opposed to the program of Khatami's government, which recently announced plans to privatize 75 percent of state-owned companies. As a result, Khatami can't mobilize workers against Khamanei and the hard right without undermining his economic program--so he's stood by, as the right wing has blocked his agenda.</P>
<P>The student protests--which were triggered, after all, by Khatami's privatization--are a sign of bitterness over failed reforms, as well as hostility to the right. Meanwhile, Washington is trying to take advantage of the crisis by stepping up pressure over Iran's nuclear energy program.</P>
<P>Secretary of State Donald Rumsfeld has even asserted--without evidence--that Iran possesses nuclear weapons. This intervention must be opposed. </P>
<P>Anyone who thinks Washington is interested in democracy in Iran should look at Iraq, where the U.S. colonial administrator plans to handpick a government to America's liking. The real hope for a democratic transformation of Iran lies with the protests of students and struggles of workers.</P>


