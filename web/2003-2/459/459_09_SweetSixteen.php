<P><font face="Times New Roman, Times, serif" size="5"><b>Sweet Sixteen</I> and struggling to survive</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 4, 2003 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2"></font><B><I>Sweet Sixteen</I>, </B>directed by Ken Loach, written by Paul Laverty, starring Martin Compston, Michelle Coulter, Annmarie Fulton and William Ruane.</P>

<P><font face="Times New Roman, Times, serif" size="3">BRITISH SOCIALIST filmmaker Ken Loach is known for his gripping movies about the lives of ordinary people struggling to survive in a world where the deck is stacked against them. His latest movie, <I>Sweet Sixteen,</I> is no exception.</P>
<P>Set in the working-class Scottish town of Greenock, the film follows 15-year-old Liam, anxiously waiting for his mother Jean to be released from prison--where she's serving time on drug charges after taking the fall for her abusive boyfriend Stan. Trying to protect his mother, Liam refuses to smuggle drugs to her as Stan orders. And when Stan beats Liam as payback, the teen tries to find refuge with his older sister Chantelle and her small son.</P>
<P>Setting his heart on buying a trailer near the river for his mom, Liam and his best friend Pinball steal heroin from Stan to sell on the street. But when it turns out that they're cutting in on the business of a local drug lord, Liam is drawn deeper into the business.</P>
<P>Liam's boundless optimism for the dream of a new life with his mother again and again comes into crushing conflict with reality as he's driven to more desperate acts to protect his dream. In a powerful scene, Loach weaves images of Liam selling drugs with a voiceover of Liam making a cassette recording for his mother in jail, adding one of her favorite songs. "No junkies, no police. Just me and you," he tells her on the tape.</P>
<P>As film critic Roger Ebert commented, <I>Sweet Sixteen</I> "could take place in any American city, in this time of heartless cuts in social services and the abandonment of the poor. I saw the movie at about the same time our lawmakers eliminated the pitiful $400 per child tax credit, while transferring billions from the working class to the richest 1 percent...Such shameless greed makes me angry, and a movie like <I>Sweet Sixteen</I> provides a social context for my feelings, showing a decent kid with no job prospects and no opportunities, in a world where only crime offers a paying occupation."</P>
<P>The performances are amazing, especially given that most of the cast are completely new to acting. Go see <I>Sweet Sixteen</I>, but be prepared to leave the theater feeling shattered--and angry--when you leave.</P>

