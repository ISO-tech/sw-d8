<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">The Supreme Court's affirmative action decision</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Should we celebrate?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 4, 2003 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">SUPPORTERS OF civil rights had reason to celebrate June 23 as right wingers were blocked from winning their goal of abolishing affirmative action. In a narrow 5-4 vote, the U.S. Supreme Court upheld affirmative action policies in admissions to the University of Michigan Law School.</P>
<P>The right-wing groups behind lawsuits that targeted affirmative action at both the Michigan law school and the university's undergraduate program were expecting to triumph. They were wrong. And George W. Bush--whose administration filed a friend-of the-court brief on behalf of the affirmative action opponents--was forced to issue a statement commending the justices for "recognizing the value of diversity on our nation's campuses."</P>
<P>Still, this was only half of the court's split decision. While the justices upheld affirmative action at the law school, its ruling on Michigan's undergraduate program opened the door for a new series of attacks.</P>
<P>In a 6-3 vote, the court ruled that Michigan's undergraduate admissions policy--a point-based system that awards 20 points to African American and Latino applicants--constitutes a quota system. This further strengthened Supreme Court ruling in the 1978 <I>Bakke </I>case that, while it upheld affirmative action, banned the use of numerical factors to implement it.</P>
<P>Unfortunately, some liberals hailed the court's decision as an unqualified victory. A July 14 <I>Nation</I> magazine editorial argued, for example, that the negative ruling for the undergraduate program was "a small price to pay for preserving access to colleges and universities for members of disadvantaged groups."</P>
<P>This illustrates just how far the debate over affirmative action has moved to the right. A vague commitment to "diversity" is considered a victory--while any systematic method for actually measuring diversity remains vilified and banned as a "quota system."</P>
<P>In reality, the potential for winning support for an uncompromising defense of affirmative action is greater than it has been in years. Public sentiment has shifted toward supporting such policies over the last eight years, since Texas and California gutted affirmative action in education and hiring.</P>
<P>A January <I>New York Times/</I>CBS News poll found that 53 percent of those surveyed favored programs that "make special efforts to help minorities get ahead" to compensate for past discrimination. And a 50,000-strong march in front of the Supreme Court building in April gave voice to the brewing anger over the attack on civil rights.</P>
<P>Later in the week after its affirmative action decision, the Supreme Court also overturned a Texas law that made sodomy illegal. The case centered around two Houston men who were prosecuted for having sex in their own home.</P>
<P>The justices' 7-2 decision was a direct reversal of its ruling in the <I>Hardwick </I>case 17 years ago--which upheld a similar anti-sodomy law in Georgia. Gay rights supporters rightly celebrated the ruling.</P>
<P>But no one should think that the decision makes up the ground lost in recent setbacks for the fight for gay and lesbian equality. Less than a decade ago, expectations for real change ran high as Democrat Bill Clinton took over the White House. But he rewarded his gay and lesbian supporters by agreeing to the miserable "don't ask, don't tell" policy on gays in the military--and signed the anti-gay Defense of Marriage Act.</P>
<P>Nevertheless, the Supreme Court decision on the Texas law--like the one on affirmative action--does show that the justices aren't immune to public pressure and changing political attitudes. This is important to remember, especially with rumors flying of planned retirements by one or more justices and speculation about a fight over what right winger Bush will try appoint to fill any vacancy. The most important way to influence the decisions of the Supreme Court is to build the largest possible movements for equality and justice.</P>


