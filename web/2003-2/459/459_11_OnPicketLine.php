<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 4, 2003 | Page 11</font></p>

<p><font face="Times New Roman, Times, serif" size="4"><b>Washington, D.C. teachers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jennifer Satlin and Jeff Bale, WTU, AFT Local 6</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--More than 1,000 angry teachers rallied in front of the mayor's building June 16 to protest planned budget cuts and a school voucher program. The main demands were for the city to honor its current contract with teachers, which includes a 9 percent raise beginning October 1 and long-overdue retroactive checks for this year's raise.</P>
<P>After a short picket, teachers poured into Pennsylvania Avenue, stopping traffic. Later, they marched into City Hall itself, chanting loud enough to be heard a block away. The building's security guards, obviously on the side of teachers, showed the 400-plus protesters where to go, as they stormed into the mayor's office.  </P>
<P>Teachers took a step further and threatened a fall sick-out if their demands aren't met. The chant of the day was "No 9 percent October 1, no work October 2!"</P>
<P>The demonstration showed that teachers are coming to the conclusion that solidarity and job actions are needed to win. "Protest is good, but they'll respond to action--when we make daily operations impossible," said D.C. school librarian Greg Bargeman, who was at the rally.</P>
<P>Teachers also criticized school vouchers, which have been supported by both the mayor and the school board president--even though D.C. voters have rejected vouchers twice, and studies show that they don't improve student achievement.</P>
<P>The D.C. City Council has already slashed $100 million from next year's budget. Yet Mayor Anthony Williams is proposing $339 million in public funding for a baseball stadium for the relocated Montreal Expos. "They have millions for tax cuts for the rich, they have millions for a stadium, and our schools are literally crumbling to the ground," said teacher and ISO member Jesse Hagopian, as he spoke to the rally.</P>
<P>The Washington Teachers Union (WTU) recently suffered a scandal of monumental proportions. WTU leaders allegedly plundered the union treasury of $5 million during a 7-year shopping spree on furs, china, fine jewelry and other luxury items. </P>
<P>The scandal left teachers disgusted and demoralized. But the June 16 action--the biggest union event in at least a decade--was a major step toward overcoming this. </P>
<P>The rally happened because of pressure on union leaders--and succeeded because of rank-and-file anger. But anger alone won't be enough to keep our union focused on struggle. Only an organized rank and file can keep the pressure on.</P>
