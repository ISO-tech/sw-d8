<p><font face="Times New Roman, Times, serif" size="4"><b>Claremont Resorts and Spas</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Kathryn Lybarger</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">BERKELEY, Calif.--On a sizzling hot June afternoon, about 150 workers and community members turned out to picket the Claremont Resorts and Spa. Hotel workers, represented by Hotel Employees and Restaurant Employees Local 2850, are fighting for a fair contract--and to win union recognition for another 140 workers employed at the hotel's spa and salon. </P>
<P>KSL, the corporation that owns the ritzy hotel, has offered a meager proposal to its union food and beverage workers, who have to pay out of pocket for health insurance. Management also hired a full-time consultant to greet every new employee at the spa with an anti-union training session.</P>
<P>"It's hard to get new hires to see that they will do everything they can to keep us from getting benefits and wage raises," says Norine Madrid, an esthetician at the spa for 10 years. "But the consultant doesn't work with us everyday, and a key factor [in building the union] is in knowing one another and building relationships. I think we're going to win."</P>
<P>Community support for the struggle is strong. Most recently, the University of California -Berkeley football team agreed to join the boycott against the hotel. But building solidarity among workers at the Claremont is the most important job. When the third bargaining unit starts to go without its contract in September, management will really feel the heat.</P>

