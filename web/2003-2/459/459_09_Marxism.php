<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Proving Marxist ideas into practice</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 4, 2003 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">I WAS at a meeting on Marxism the other day, and someone asked, "How do you know if your ideas are correct?" Good question. What, indeed, is there to recommend Marxism over any other view of society and how to change it?</P>
<P>For Marx and Engels, the question of whether this or that view of the world was correct or not was something that had to be tested against experience. "The question whether objective truth can be attributed to human thinking," Marx wrote, "is not a question of theory but is a practical question. Man must prove the truth, i.e., the reality and power, the this-worldliness of his thinking <I>in practice</I>."</P>
<P>Debating the truth or non-truth of any idea without reference to the real world "is purely a scholastic question." Just as in the "hard" sciences, the truth of this or that scientific theory is tested successfully or unsuccessfully in experiments, so theories of society must also be tested.</P>
<P>We can sit for hours debating whether or not socialism--the collective ownership of the means of production by the associated producers--can be achieved through the ballot box. But the answer has already been provided by a number of practical experiments in which socialist parties succeeded in getting elected only to find that they themselves were changed by the system rather than changing it. The compromising role played by left candidates, moreover, is itself clear proof of the Marxist analysis that the state is not a neutral body, standing over society, but rather the instrument for the maintenance of the rule of one class over another.</P>
<P>Marxism is therefore very different from religion, which asks that its followers accept its ideas on faith, or from utopians, who simply counterpose what exists to what ought to exist. "Communism is for us," wrote Marx and Engels, "not...an <I>ideal</I> to which reality [will] have to adjust itself. It is "the <I>real</I> movement which abolishes the present state of things."</P>
<P>Marxism was based on the idea that capitalism's own social and class contradictions--marked by recurring economic crisis in spite of the tremendous development of social wealth, and by a working class whose struggles are of necessity collective and social rather than individual--created the conditions for a new society. The starting point of Marx and Engels, therefore, was not "what men say, imagined, conceived, in order to arrive at men in the flesh."</P>
<P>They "set out from real, active men, and on the basis of their real-life process demonstrating the development of the ideological reflexes and echoes of this life-process." Our ideas are social, rather than individual, products.</P>
<P>The point was not that it was impossible to have ideas about freedom before the conditions for their realization existed, or that there was a mechanical one-to-one relationship between people's ideas and their material conditions of life. However, "one cannot be liberated," if one is "unable to obtain food and drink, housing and clothing in adequate quality and quantity."</P>
<P>"Liberation," they argued, "is a historical and not a mental act."</P>
<P>It's true, someone might argue, that reformism has never worked. But your method--collective revolution--also failed. Just look at Russia. But the truth is that a materialist analysis of the failure of the Russian revolution explains clearly why it failed.</P>
<P>Socialism must be based on abundance. But the conditions for abundance in 1917 existed only on a world scale--not within the confines of an isolated Russia. Revolution could begin in Russia, but it had to be finished elsewhere in order to be consummated, because on a national scale the material conditions for the achievement of socialism did not exist in Russia.</P>
<P>As Marx wrote in the<I> German Ideology</I>, anticipating this problem: "This development of the productive forces...is an absolutely necessary practical premise, because without it privation, <I>want </I>is merely made general, and with <I>want</I> the struggle for necessities would begin again, and all the old filthy business would necessarily be restored."</P>

