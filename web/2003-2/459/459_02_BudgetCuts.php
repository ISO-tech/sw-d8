<P><font face="Times New Roman, Times, serif" size="5"><b>State budget crisis out of control</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 4, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">STATE POLITICANS across the U.S. are swinging the budget ax. According to a survey released by the National Governors Association and National Association of State Budget Officers, over the last year, 37 states chopped a total of $14.5 billion out of their budgets by cutting social services and laying off workers.</P>
<P>"Unfortunately, the news is pretty bad, and I think it's going to get a little worse," said Ray Scheppach, executive director of the governors' group. According to Scheppach, the state budget crisis is not only as bad as what was faced during the Second World War--it's as bad as the War of 1812. "It's clearly the worst [crisis] since we've been keeping statistics," he said.</P>
<P>At least half of U.S. states are planning to freeze hiring. More than a dozen are laying off workers, and at least 10 plan to increase college tuition costs.</P>
<P>In Florida, legislators cut spending for higher education, nursing homes and the poor. They tapped money from trust funds set aside for the environment and affordable housing and passed $100 million in fee increases and an 8.5 percent tuition increase. In New Jersey, legislators struck a last-minute budget deal that includes reducing aid to colleges and universities and cutting state government operations. </P>
<P>But no state is in worse shape than California, which faces $38.2 billion in red ink.
Emergency funds that the state recently borrowed will run out by mid-August. If state legislators can't agree on a budget--and they remained deadlocked as <I>Socialist Worker </I>went to press--the state will begin mandatory layoffs and stop paying for community colleges and child care for poor families. Already, the Los Angeles Community College District, which has 130,000 students, has eliminated classes, laid off faculty members and is considering raising tuition by 50 percent. </P>
<P>Nursing homes that they will have to shut down or limit their services. "[Residents] see what's going on in the newspapers and on TV," Betsy Hite of the California Association of Health Facilities told a reporter. "Their perspective is why are they doing this to us? What did we do? If I were a betting person, I wouldn't bet we're going to be fine. The gap is just too huge."</P>


