<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>How can unions stop their decline?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 4, 2003 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">HOW CAN labor reverse its decline--and what role will rank-and-file activism play in a turnaround? Those pressing questions will be posed even more sharply this summer as unions face a series of key contract battles.</P>
<P>In August, the Communications Workers of America (CWA) face a tough contract showdown with Verizon as the telecommunications giant seeks to reverse the union's gains won in strikes in the two previous contracts. In September, the United Auto Workers' (UAW) contracts expire with the Big Three automakers--and employers are demanding a freer hand to close plants as well as slash medical benefits and pensions.</P>
<P>Most union leaders appear ready to accept concessions in the name of "partnership" with employers. The UAW, for example, recently agreed to allow parts maker Metaldyne Corp. to purchase an auto parts plant in Indiana from DaimlerChrysler--and slash wages there from $26 to $16 per hour. In return, Metaldyne will allow "card check" at its other nonunion parts plants--that is, agree to recognize the UAW if a majority in a workplace sign union cards. </P>
<P>Not every union has accepted the drive for concessions without a challenge. At Verizon--where the union has won two strikes in recent years--the CWA last year conducted a multimillion-dollar public relations campaign against the company's planned layoffs.</P>
<P>The union challenged the company's claims of poverty and rightly accused management of violating the contract. Yet union leaders themselves stuck to the letter of the deal--and simply folded when the layoffs went through. That's because while the CWA leaders may have a different style than their counterparts in the UAW, they, too, accept the boundaries of labor-management partnership.</P>
<P>This is true of virtually the entire bureaucracy of full-time professional union negotiators. They want to maintain unions to ensure their own interests, yet tend to shy away from risks that could endanger their relationships with employers.</P>
<P>But by trying to avoid a fight today, unions are only guaranteeing a bigger confrontation--and against much worse odds--in the future. The case in point is GE, where union membership is a mere fraction of its former strength.</P>
<P>After a contract mobilization this spring, the coalition of unions at the company set a strike deadline--but settled once GE pulled back on its more outrageous demands for concessions. While the deal doesn't contain the givebacks seen, for example, in the airlines, it won't reverse the union's decline at the world's biggest manufacturing company.</P>
<P>GE was once a stronghold of the left-led United Electrical workers (UE) in the 1930s and 1940s, but during the anticommunist witch-hunts of the 1950s, the labor federations expelled left-wing unions. UE, for example, saw its membership carved into rival unions.</P>
<P>Thousands of socialists, communists and other left-wingers--the rank-and-file leaders of the organizing drives and strikes of the 1930s--were not only fired from their jobs, but driven out of the unions by top officials. These activists had been the most effective workplace leaders--not just because of their organizational skills, but because of their politics.</P>
<P>As socialists, they rejected partnership--or class collaboration, as they called it. Even during the Great Depression of the 1930s, they argued that the big corporations could afford to pay better wages and improve conditions--and they used militant tactics like the 1936-37 UAW sit-down strike at General Motors, defying the law when necessary.</P>
<P>If union officials said such goals were too risky or unrealistic, rank-and-file activists organized to put pressure on them--or took action on their own, without waiting for official approval. If it's hard to imagine how such activism could revive today, it's important to recall that the big victories of the 1930s were preceded by years of frustrating defeats.</P>
<P>None of the activists who made those victories possible could have predicted when the next big battle would break out--nor was success guaranteed. But through patient work in conditions far worse than today's, they were able to lay the groundwork for later victories.</P>
<P>Today's rank-and-file activists need to look beyond the narrow framework of accepted by union leaders. By rediscovering labor's fighting past--and renewing those traditions--we can begin preparing now for the crucial battles of the future.</P>


