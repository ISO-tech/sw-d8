<P><font face="Times New Roman, Times, serif" size="5"><b>Whose world? Our world!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 4, 2003 | Page 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3">"WHOSE WORLD? Theirs or ours?" More than 900 people answered that question with a resounding "ours" at last weekend's Socialism 2003 conference in Chicago, cosponsored by the International Socialist Organization.</P>
<P>In one of the largest gathering of revolutionary socialists in the U.S. since the 1970s, people came from across the country to discuss and debate political questions at more than 100 meetings and workshops--on topics ranging from the fight for civil rights, to U.S. imperialism today, to the history of the Russian Revolution. "I think it was great," said Helen Walls, an activist from Chicago. "For me, not being exposed to a lot of these ideas before, this is the first time I've felt so positive about the idea that you can change the world."</P>
<P>Among the featured speakers were Palestinian author Naseer Aruri, <I>Democracy Now!</I> correspondent Jeremy Scahill and exonerated death row prisoner Madison Hobley. "I was incredibly impressed with the vivacity, the energy, the desire to learn, the humbleness, the openness and warmth," said Toufic Haddad, a Palestinian-American and coeditor of the journal <I>Between the Lines, </I>who traveled from his home in the West Bank to speak at the conference. "People relate to each other very deeply here. There's something so wonderful about it."</P>
<P>International solidarity was the hallmark of the conference--with representatives from the front lines of struggle in Brazil, Venezuela, Puerto Rico, France and elsewhere.</P>
<P>"There really is something electrifying about what is taking place," <I>Socialist Worker </I>columnist Sharon Smith told the crowd at the final rally. "I think it reflects what's happening for the left around the world--that the left is beginning to grow and is beginning to realize what's important for our struggle."</P>


