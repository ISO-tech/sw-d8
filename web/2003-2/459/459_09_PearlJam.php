<P><font face="Times New Roman, Times, serif" size="4">Pearl Jam takes political dissent on the road</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Reading Dubya the riot act</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Jabiz Raisdana</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 4, 2003 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ON APRIL 1, Pearl Jam fans were treated to a bit of theater at their show in Denver, Colo., when lead singer Eddie Vedder donned a George W. Bush mask and launched into "Bu$leaguer" from the band's most recent album <I>Riot Act.</I> "A confidence man, but why so beleaguered?/He's not a leader, he's a Texas leaguer/Swinging for the fence, got lucky with a strike/Drilling for fear makes the job simple/Born on third, thinks he got a triple."</P>
<P>Vedder removed the mask, placed it on the microphone stand and sang to it. By the end of the song, he'd pushed it to the floor and started attacking it. The next day, newspapers reported that hundreds of angry fans booed and stormed out of the venue. But organizers and band members later said that the exodus was exaggerated, and only a handful of people exited in protest.</P>
<P>Challenging their audience with politically charged lyrics is nothing new for Pearl Jam. Since the release of their first album, <I>Ten, </I>the band has resisted every attempt by big business record companies to silence their political voice.</P>
<P>In 2000, Vedder supported Ralph Nader's presidential campaign by touring with the Green Party candidate and other musicians and activists like Patti Smith and Michael Moore. In 1994, at the height of its success, Pearl Jam shocked the music industry by taking on Ticketmaster, the ticket distribution giant, in an effort to make its concerts more affordable to fans. The band canceled its summer tour and testified before Congress opposing Ticketmaster's monopoly on ticket sales.</P>
<P>To the dismay of their record company, Epic, Pearl Jam is releasing every show from their current tour on a double, or sometimes triple, CD for only $14.98. Fans can go to the fan club web page, www.tenclub.net and order any show, and it will be mailed to them within days of the event.</P>
<P>The CDs, however, can't compare with seeing Pearl Jam live. In addition to their own material, they've bolstered their sets by regularly covering John Lennon's "Gimme Some Truth," Creedence Clearwater Revival's "Fortunate Son" and The Clash's "Know Your Rights."</P>
<P>After audience members in Uniondale, N.Y., began chanting "USA!" and booing when Vedder sported the Bush mask, the band began to sing Tom Petty's "I Won't Back Down" while continuing to play "Bu$leaguer." In a time when dissent of any kind can get your show canceled or your music banned from the airwaves, Pearl Jam refuses to stand down.</P>
<P>If you haven't listened to Pearl Jam in a while, you should revisit the band. They've matured into a political force. "Well I guess there's nothing wrong with what you say/But don't sell me, "There can't be better ways"/Tell the captain "This boat's not safe, and we're drowning"/Turns out he's the one making waves."</P>


