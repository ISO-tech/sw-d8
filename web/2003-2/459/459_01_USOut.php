<P><font face="Times New Roman, Times, serif" size="4">End the occupation</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bush calls this freedom</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 4, 2003 | Page 1</font></p>

<P><font face="Times New Roman, Times, serif" size="3">"OPEN THIS fucking door! Do you understand me?" With these words--screamed in English--U.S. Marines smashed their way into another Iraqi home during the early morning hours last week, in a scene captured by ABC News cameras. But inside, all the soldiers found were terrified women and children.</P>
<P>This was the reality of "Operation Sidewinder"--the daily raids across Iraq that were supposed to root out so-called "insurgents" who have been attacking U.S. soldiers occupying the country. According to the White House warlords, the Iraqi attacks are the work of "international terrorists" and the "remnants" of Saddam Hussein's regime.</P>
<P>But even the corporate media have begun to recognize the mass scale of the resistance to U.S. colonial rule in Iraq. And when it comes to "international terrorism," the Pentagon has no rivals.</P>
<P>Ask Iraqi businessman Khraisan Al-Abally. He told a U.S. reporter that, during a raid on his home, American troops shot his brother and took his 80-year-old father into custody. Then they forced him to kneel naked and kept him bound hand and foot, with a bag over his head--on and off for <I>eight days.</I> "I thought I was going to lose my mind," Al-Abally said.</P>
<P>And Al-Abally isn't alone. According to a report issued last week by Amnesty International, hundreds of Iraqis held at U.S.-run prison camps have been barred from seeing their families--and denied sleep and even water. Meanwhile, three months after the collapse of the old regime, life is harder than ever for Iraqis. According to a new report by the United Nations World Food Program, 100 percent of Iraq's 27 million people are now dependent on food aid.</P>
<P>This systematic humiliation is the source of the resistance to the U.S. occupiers--not the mythical Saddam "loyalists" that the Bush administration talks about. If anything, U.S. officials are the ones responsible for handing power <I>back </I>to certain former "Saddam loyalists." Like Hussein Jassem Ijbara, the ex-Republican Guard general--who was returning to his home in Amer when he was informed that U.S. soldiers had it surrounded. </P>
<P>Did they want to arrest him? Not at all. "Instead, they asked me if I would agree to be the governor of the Salahadin province," Ijbara told the <I>Financial Times</I> last week. "I don't think it matters whether they trusted me. We have a system now very much like they have in the United States. Our province is like an American state. In other words, I have all the power."</P>
<P>As in any war, it is ordinary American soldiers--not the Pentagon brass or the Washington chicken hawks--who suffer the casualties in the armed attacks on U.S. troops, now an almost daily occurrence. As the weeks drag on, more and more soldiers will come to question why they are killing--and being killed--in a war to pump up U.S. oil profits and expand Washington's power.</P>
<P>"U.S. officials need to get our asses out of here," Staff Sgt. Charles Pollard, a 43-year-old reservist from Pittsburgh, told the <I>Washington Post</I>. "I say that seriously. We have no business being here."</P>
<P>Pollard is right. The U.S. has no honest business in Iraq--and never did. We need to expose the truth--and build the fight to put an end to Washington's brutal occupation.</P>


