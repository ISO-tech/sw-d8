<P><font face="Times New Roman, Times, serif" size="4">Obituary</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Costella Cannon</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 4, 2003 | Page 4</font></p>

<B><font face="Times New Roman, Times, serif" size="3"><P>Dear <I>Socialist Worker,</I> </P>
</B><P>We were so sad to learn that Costella Cannon passed away on June 21 from cancer. Costella was a tireless fighter for abolition who was an inspiration to all of us in the Campaign to End the Death Penalty. </P>
<P>Her son, Frank Bounds, who was unjustly convicted and sentenced to death, died of medical neglect on Illinois' death row years ago. But Costella continued to fight to clear Frank's name and sought justice for all her "sons and daughters on death row." </P>
<P>"The reason for all my zeal and all my energy is because of Frank," she once said. "Every time I march, I'm marching for Frank Bounds and what he stood for. And I'll keep fighting until we can be sure there are no innocent men on death row. The fight has really just begun."</P>
<P>Costella spoke out about the injustices of the death penalty at rallies, press conferences and other forums all over the country. When she spoke, her booming voice captured the attention of everyone in the audience--and oftentimes would bring the crowd to their feet. She spoke from the heart, and she spoke about what was right and what was wrong. </P>
<P>In 2000, Costella was featured in <I>Socialist Worker's</I> coverage of the fight against the death penalty in Illinois. I remember she was so proud of being featured in <I>SW</I> that she bought a bunch of copies to give out.</P>
<P>She always had the courage to stand up for justice. Her life is a testament to struggle. We will remember Costella as a fighter, a dear friend and a source of inspiration to our movement. She will be missed. </P>

<B><P>Alice Kim, </B>Chicago</P>

