<p><a name="Prison"></a><font face="Times New Roman, Times, serif" size="4"><b>Brutality of prison after death row</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P><B>Dear <I>Socialist Worker,</B></I></P>
<P>I want to show my support for possibly the best newspaper in the world by sharing my story with you and the world.</P>
<P>I was under the sentence of death for over 12 years, and 11 of those years were spent at the Pontiac human warehouse. My sentence was commuted to life without the possibility of parole on January 10, 2003, by former Illinois Gov. George Ryan. Myself and 11 others were transferred from the Pontiac human warehouse to here--the Menard human warehouse on January 28.</P>
<P>Readers and those of you out there who stand against all forms of oppression inflicted upon your human brothers and sisters behind these walls, rest assured: former Gov. Ryan kept his word when he said that "life won't be anywhere near easy for those individuals" whose sentences he commuted to life. After being transferred off death row, I was assigned to a unit of chaos and confusion. I am confined to a cage with only a former death row prisoner. </P>
<P>I might get the chance to go to the yard twice a week--when I was on death row, I was allowed to go to the yard six days a week. I am only allowed to eat my lunch or dinner for 15 minutes when I go to the mess hall. </P>
<P>I have to buy shampoo from the commissary to clean the cage that I am confined to. On death row, I was given cleaning supplies. I might get a 15-minute shower twice a week. When I was on death row, I was given a shower six days a week.</P>
<P>I might get the chance to use the telephone once a week to call my family whenever I am allowed to go to the yard. It wasn't like this on death row. I am not allowed any kind of job assignment. I barely sleep now. I am being discriminated against, and worst of all, my attorney was dropped from my case.</P>
<P>I give the former governor a triple "A+" for effort, because he did spare many lives. However, I did not ask for this undercover punishment. I asked for a full pardon or nothing at all.</P>
<B><P>Dedrick Coleman, </B>Menard, Ill.</P>



