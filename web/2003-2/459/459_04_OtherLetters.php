<P><p><font face="Times New Roman, Times, serif" size="4"><b>We deserve better than the "lesser evil"</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P><B>Dear <I>Socialist Worker,</B></I></P>
<P>Donald Williams argues that our world can't "tolerate another four years of Bush, Cheney, Aschcroft, Perle, Rove and Rumsfeld." (<I>SW</I>, June 20) I couldn't agree more--a huge swath of the world's population is already refusing to tolerate these gangsters in blue suits.</P>
<P>From the more than 500,000 people in New York City who protested against the invasion of Iraq on February 15 to the millions of Iraqis who are struggling against an occupying army, people are standing up to Bush. And Bush's attacks on working-class people will drive more folks to fight back.</P>
<P>But I have to disagree with Williams on something else--he, like many people on the left, is arguing that we have to vote for a Democrat as the lesser evil in 2004. The problem is that supporting Democrats is how we wound up with Bush in the first place.</P>
<P>If folks remember what happened in Florida in 2000, while outraged people pointed out the disenfranchisement of Black voters and the undemocratic nature of the Electoral College, Al Gore only put up a tepid fight focusing on "hanging" and "pregnant" chads. And when the U.S. Supreme Court installed Bush as commander-in-thief, Gore admonished the nation to follow its new leader--presumably to war, layoffs and massive cuts in social spending.</P>
<P>Was Al Gore just spineless? Not exactly. He and the rest of the Democratic Party leadership represent the same corporate interests as Bush's gang of warmongers and ex-CEOs. And those interests were more important than actual democracy.</P>
<P>The Democratic Party led the way into every major war of the 20th century except the first Gulf War--from the First World War to Vietnam. Lyndon Johnson ran as the peace candidate in 1964, but then massively escalated the Vietnam War.</P>
<P>During the strike wave of the 1930s, Franklin Roosevelt tried to trick striking autoworkers occupying a factory into handing it back to their bosses. If they had listened, they would have lost the strike that led to the building of the United Auto Workers union.</P>
<P>Sure, there's a difference between Democrats and Republicans. Democrats will present themselves as moderate, liberal or even left wing if they're worried about pressure from below. Sometimes, they even support protests and strikes. But calling for a vote for the Democrats retards the grassroots movements that push politicians in both parties to do what we want.</P>
<P>For every evil thing that Bush has done, there were plenty of Democrats willing to jump on the evil bandwagon--from the USA PATRIOT Act, to the war on Iraq. If the Democrats have the left safely in their pocket, they will continue to move right.</P>
<P>And the system will continue to belch up the same politicians that send workers off to kill other workers in war. The only practical solution is to build our own party--one that isn't "evil" at all.</P>
<B><P>Adam Turl, </B>Chicago</P>
