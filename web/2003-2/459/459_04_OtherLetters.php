<p><a name="ILWU"></a><font face="Times New Roman, Times, serif" size="4"><b>ILWU took a dive in our contract fight</b></font></P>

<B><font face="Times New Roman, Times, serif" size="3"><P>Dear <I>Socialist Worker,</B></I></P>
<P>Aloha from Hawaii, home to ILWU Local 142. I can empathize with ILWU Local 10 business agent Jack Heyman (<I>SW</I>, January 10) about the new contract negotiations and what came out of it. My frustration level might be even higher than Heyman's. </P>
<P>Whatever the West Coast workers got in the contract, we in Hawaii got even worse--and in my opinion without even putting up much of a fight. If it were a boxing match, people would say that it was fixed. </P>
<P>If you're going to take a dive, at least go a few rounds to give the impression that you gave it a shot. Don't take a dive in the first round like our people did. </P>
<P>Our union likes to preach "same work, same pay." Too bad they don't actually follow that rule. I think in some ways, our union leaders have become politicians. </P>
<P>And I have to wholeheartedly agree that this new contract was in no way a major victory. Not in Hawaii anyway.</P>
<B><P>Robert McDurmin, </B>From the Internet</P>
