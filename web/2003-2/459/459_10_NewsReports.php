<p><a name="Ryan"></a><font face="Times New Roman, Times, serif" size="4"><b>Free Ryan Matthews</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul Grohman</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SUPPORTERS OF wrongly convicted Louisiana Death Row inmate Ryan Matthews gathered June 20 at St. Mary's Church in New York City. The event, sponsored by the Campaign to End the Death Penalty, featured Ryan's mother Pauline Matthews, and his lawyer Billy Sothern.</P>
<P>Matthews was sentenced to die in May 1999 after a three-day trial for a crime he was arrested for when he was merely two weeks past his seventeenth birthday. Ryan, who is African American, has maintained his innocence in the murder of a white convenience store owner just outside of New Orleans. The prosecution of a juvenile offender like Ryan defies basic notions of human rights--and distinguishes the U.S. from nearly every other country in the world.</P>
<P>As with many death penalty cases, Ryan's case was infused with racism. His jury had 11 white members and one Black member in spite of the fact that he county where the trial was held was 30 percent minority.</P>
<P>Pauline Matthews spoke of her ordeal. "I felt that the courtroom was prejudiced and racist, the atmosphere reminded me of a lynching, and they lynched my son in that courtroom."</P>
<P>Speaking of the cruel treatment of her son, she said "Ryan has a seizure disorder and requires medication to control it--and he was not regularly given his medication in prison. Ryan is in his cell 23 hours a day, and is always handcuffed and shackled. They wanted Ryan to plea bargain, but Ryan said that he hadn't done anything, and wouldn't plea bargain. I felt like in doing this, we had won."</P>
<P>Evidence at the trial showed that two different DNA samples were found on the mask that was worn during the crime--but neither matched Ryan. Plans are in the works to protest at a hearing for Ryan in August in New Orleans. To find out more about Ryan's case and others and what you can do to help, check out www.reprieve.org.</P>

