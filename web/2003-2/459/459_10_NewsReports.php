<p><font face="Times New Roman, Times, serif" size="4"><b>Stop Bush at home and abroad</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Wrigley-Field</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Several thousand people rallied June 23 against George W. Bush outside the Sheraton Hotel where he was holding a $2,000-a-plate fundraiser. The fundraiser made $4 million for Bush's 2004 presidential campaign at a time when he's pushing through tax cuts for the rich and cuts in social services for the poor. Gov. George Pataki, Mayor Michael Bloomberg and former Mayor Rudolph Giuliani joined Bush for the occasion.</P>
<P>The Planned Parenthood Federation of America called the protest, and other organizations jumped on board, including United for Peace and Justice. Especially prevalent were signs reading "Warning: Bush's policies dangerous to women's health."</P>
<P>Several speakers also connected Bush's attacks on abortion to his wars abroad. "Bush is waging a war against us, and in this case against poor women, working-class women and women of color, who need reproductive services most," said Jean Carey Bond, a speaker from the Black Radical Congress. "This is our answer to Bush's promise of endless war. Mr. Bush, we promise you we will not go back to illegal abortions, to all-white schools and workplaces, to McCarthyism."</P>
<P>Many protesters expressed anger at Bush's policies in general. "I can't imagine anyone doesn't feel things are going profoundly wrong in the U.S. I ask pro-war people, 'why did we go to war?' And there's never a satisfactory answer." said Thomas, who came to the protest with a large banner reading "Free Palestine." </P>
<P>City council member Charles Baron attacked Bush's war for oil and then announced: "I do hereby make you all sheriffs, and I charge you to arrest Bush for his war crimes." So when police began to arrest protesters, hundreds of people in nearby pens chanted "Arrest George Bush!"</P>
<P>In all, six people were arrested throughout the day. "[I want to tell people to] take sides," said protester Joe Carranza. "Speak out before you are gagged with the American flag. Silence is always to the benefit of the oppressor."</P>
