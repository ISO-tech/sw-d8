<p><a name="Cops"></a><font face="Times New Roman, Times, serif" size="4"><b>Stop the attacks on civil liberties</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Peter LoRe</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--About 80 people crowded into the southwest corner of Union Square June 14 to protest the attacks on our civil liberties and the recent deaths of Alberta Spruill and Ousmane Zongo. Spruill and Zongo--both unarmed and Black--became the two most recent of nine people killed by New York City police in the first sixth months of this year. </P>
<P>Armed with signs taking up a wealth of issues--"Repeal the PATRIOT Act," "Stop racist police," "End the death penalty"--protesters gave speeches for an hour and then marched from Union Square to Tompkins Square. Speakers talked about the assault on civil liberties as well as the deportation of immigrants rounded up by the Bureau of Citizenship and Immigration Services (formerly known as the INS).</P>
