<font face="Times New Roman, Times, serif" size="5"><b>Vermont nurses win big</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul Fleckenstein and Kathleen Brown</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 4, 2003 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">BURLINGTON, Vt.--Unionized nurses won a major victory June 21 at Fletcher Allen Health Care (FAHC), the largest hospital in the state. In their first contract after winning a union representation last October, the 1,200-member Vermont Federation of Nurses--an affiliate of the American Federation of Teachers (AFT)--prevailed on the key workplace issues.</P>
<P>An afternoon rally of 500 nurses and community supporters called to support the union in contract talks turned into a victory celebration when the FAHC agreed to the most important contract demands just hours earlier. These included limits on how many patients nurses must care for, an end to mandatory overtime, no forced movement of nurses to care areas outside of their trained expertise and strong union workplace provisions covering both current employees and new hires.</P>
<P>With this contract, FAHC will be one of only 10 hospitals in the U.S. with binding safe staffing levels covering all nurses. Registered nurse Barb Segal announced that this was the "best safe patient care contract in the U.S. today." AFT national vice-president Nat LaCour called this "one of the biggest wins in two decades."</P>
<P>A main complaint for nurses has been too many patients and forced overtime that creates stressful and unsafe conditions. "I feel very gratified," said Jo Derosier, a nurse for 13 years at FAHC. "We're in this for patient care. It makes quality care a lot better."</P>
<P>The contract includes small wage increases--2 to 4 percent on base wages of $20 to $30 dollars an hour--as well as benefits for part-time nurses, layoff protections, automatic pay increases and an option to reopen wage negotiations after two years. With the union security provision, Segal told the cheering crowd that "we will get stronger, stronger, stronger--we will stand united."</P>
<P>The union worked closely with religious and community leaders and public officials to pressure FAHC. Many union members thanked the public for support. "The public support felt good, and to all the unions that have stood behind us--stand strong!" said Betsy Dunn of the bargaining committee. A series of roadside pickets, radio ads and a countywide lawn sign campaign during recent weeks demonstrated this support. </P>
<P>Recent scandals at FAHC including major construction cost overruns and lying to state regulators also helped convince many people that the hospital administration had lost its concern for patient care. But the union wouldn't have been successful without strong rank-and-file activism. </P>
<P>After several unsuccessful organizing campaigns in previous years, the nurses' union won by a 2-to-1 margin. Nurses then set up a 50-member bargaining committee. While some called such a large committee unworkable, one member of the committee said that the large size and broad representation were crucial to its success. "When I voted for the union this time, I knew that being active is the only way we can make things work," said one nurse.</P>
<P>Solidarity and broad support for more militant actions were also important. FAHC continued the same aggressive anti-union tactics it used to thwart previous organizing drives. In one instance, nurses quickly responded to management attacks with a public protest that forced FAHC to reinstate a veteran nurse fired for her union activism.</P>
<P>But in the final weeks, 800 of the 1,200 nurses signed a petition endorsing a range of actions, including striking, if contract talks broke down. "If you stick together, it can happen," said Barbara Callahan, a nurse for 14 years. "I'm part of a very powerful group. It's a strong victory, not only for RNs, but also for the community. This victory will start the ball rolling for workers throughout the state."</P>
<P>Hopefully, the next victory will be for 150 downtown Burlington grocery workers, who are now in bargaining for their first contract at City Market.</P>

