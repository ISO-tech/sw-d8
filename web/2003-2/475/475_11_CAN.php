<P><font face="Times New Roman, Times, serif" size="5"><b>Campus antiwar activists meet in Chicago</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Julie Keefe and Suzie Schwartz</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 7, 2003 | Page 11 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--The Campus Antiwar Network (CAN) held its second national conference last weekend at the University of Illinois at Chicago. CAN, a grassroots network of student antiwar coalitions, brought together more than 100 students from 31 campuses across the country to debate and discuss the key issues facing the movement. </P>
<P>The conference focused on bringing the student movement forward into this new phase of the U.S. war in Iraq--and followed the successful CAN/Muslim Students Association national speaking tour "Speaking Truth to Empire" and the October 25 national demonstrations that featured CAN contingents of 300 in Washington, D.C., and 150 in San Francisco.</P>
<P>Debates emerged in the educational workshops, which addressed issues such as the United Nations (UN), Palestine, the 2004 elections and the nature of the occupation of Iraq. The most vigorous debates centered around CAN's proposed points of unity. </P>
<P>The adopted points included supporting the Iraqi people's right to self-determination, calling for the immediate removal of troops from Iraq and opposition to the oppression of the Palestinians. Although a proposed point of unity calling for the end of the U.S./UN occupation failed by one vote, a resolution was passed that encouraged further discussions and debates on this issue within individual coalitions. </P>
<P>A highlight of the weekend was the presence of Omar Waraich, a student from the British Stop the War Coalition. "I feel that this could be the beginnings of a strong cross-Atlantic student antiwar movement," said Waraich.</P>
<P>Waraich also brought a proposal for a joint student delegation to Iraq, tentatively planned for next summer. CAN delegates voted to extend the national speaking tour into the spring with a new focus on greater cooperation with Military Families Speak Out and Veterans for Peace in order to connect the student movement with the broader struggle. </P>
<P>CAN and Waraich also discussed an international student day of action for early December. "This is the beginning of a prolonged student movement to end the occupation of Iraq," said Katrina Yeaw of San Francisco State.</P>


