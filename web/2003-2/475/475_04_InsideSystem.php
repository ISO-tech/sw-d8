<P><font face="Times New Roman, Times, serif" size="5"><b>Inside the system</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 7, 2003 | Page 4 </font></P>

<P><p><font face="Times New Roman, Times, serif" size="4"><b>Crazy like Fox News</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">FIRST CAME Fox News' lawsuit against comedian Al Franken's book <I>Lies (and the Lying Liars who Tell Them).</I>That was laughed out of court. But it didn't stop Fox News Channel from threatening to sue the makers of <I>The Simpsons</I> over a parody of the news channel's right-wing political stance. </P>
<P>In a recent interview with National Public Radio, <I>Simpsons</I> creator Matt Groening said Fox News had considered legal action against the show--despite the fact that <I>The Simpsons</I> is broadcast on sister network, Fox Entertainment. Fox apparently took exception took a Simpsons' version of the Fox News rolling news ticker that contained the headlines "Do Democrats Cause Cancer?" "Study: 92 percent of Democrats are gay" and "Oil slicks found to keep seals young, supple."</P>
<P>"Fox fought against it and said they would sue the show," Groening said. "We called their bluff because we didn't think Rupert Murdoch would pay for Fox to sue itself. So, we got away with it." </P>
<P>While the lawsuit never materialized, Groening said some action was taken. "Now Fox has a new rule that we can't do those little fake news crawls on the bottom of the screen in a cartoon because it might confuse the viewers into thinking it's real news," he said. <BR>
-- <B>Agence France Press, </B>October 29, 2003</P>

<p><font face="Times New Roman, Times, serif" size="4"><b>Pushing Ann Coulter's buttons</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">TALKING PRESIDENTS, manufacturers of such presidential action figures as George W. Bush and Bill Clinton, is now releasing an action figure of Ann Coulter, the right-wing nut. The figure comes complete with a series of pithy catchphrases recorded by the right-wing author herself.</P>
<P>Push the doll's button, and you'll hear such "Coulterisms" as: </P>
<P>-- "Liberals can't just come out and say they want to take more of our money, kill babies and discriminate on the basis of race."</P>
<P>-- "Why not go to war just for oil? We need oil. What do Hollywood celebrities imagine fuels their private jets? How do they think their cocaine is delivered to them?"</P>
<P>-- "Liberals hate America, they hate flag-wavers, they hate abortion opponents, they hate all religions except Islam, post 9/11. Even Islamic terrorists don't hate America like Liberals do. They don't have the energy. If they had that much energy, they'd have indoor plumbing by now."</P>
<P>Sporting a black miniskirt, the "highly collectible doll" comes in a display box with information highlighting Ann's "unique contributions to America's political discourse." <BR>
-- <B>DemocraticUnderground.com, </B>October 27, 2003</P>

