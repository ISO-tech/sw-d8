<P><font face="Times New Roman, Times, serif" size="5"><b>LA transit strikers hold the line</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs and Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 7, 2003 | Page 11 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">LOS ANGELES--Striking bus drivers were set to vote on a new contract offer from the LA Metropolitan Transit Authority (MTA) as <I>Socialist Worker </I>went to press. The 2,800 workers, members of the Amalgamated Transit Union (ATU) Local 1277, were initially told by union leaders that they wouldn't vote on the deal.</P>
<P>Instead, Local 1277 President Neil Silver said, the union would seek binding arbitration in which mechanics would return to work while a panel resolves the dispute. But MTA Chair Zev Yaroslavsky rejected arbitration, insisting on a membership vote on the deal instead.</P>
<P>Yaroslavsky is playing hardball in an effort to defeat the strike, which has shut down bus and train lines throughout the area since it began October 14. The  key issue is health care.</P>
<P>MTA officials accuse the union of mismanaging a $17 million health care plan that's funded by the agency and run by Local 1277. The real issue, however, is soaring health care costs--and the MTA's effort to push the costs onto workers.</P>
<P>After its latest offer, the MTA broke off negotiations, seeking an impasse. This would pave the way legally for management to impose its contract and start hiring scabs. In response, Silver reversed himself, announcing that he would schedule a ballot November 7 and recommend another rejection of the deal.</P>
<P>Details on the latest offer weren't immediately available, but few expected improvements from the MTA's original offer. This included a $200 monthly increase in health care premiums, a two-year wage freeze, a 70 percent cut in retiree health benefits, and increased rights to outsource union work. </P>

<p><font face="Times New Roman, Times, serif" size="4"><b>"A victory for us will be a victory for all"</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3"><B><I>Socialist Worker's </I>DAVID RAPKIN spoke to striking ATU mechanics about their struggle.</B></P>

<B><P>Joe</B> <BR>
Basically, the MTA doesn't want to contribute any more than what they've been contributing to the benefit package. It's primarily over the benefits; all this is over the benefits. We haven't had a raise since 1997. They've held back the cost of living raise for over a year. They're proposing right now not to give us a raise until this coming year. They want total control of the benefits fund. </P>

<B><P>Larry</B> <BR>
Management downtown doesn't know anything about what we do in this bus yard. We have over 270 buses fueled and cleaned every night. Management thinks that we don't do anything, because we hear that all the time we don't do anything. It's kind of hard. It hurts, when I know what we do every night.</P>

<B><P>Ken</B> <BR>
[Management says] your mother has to go to the store or to the doctor and she can't get there [because of the strike]. But management doesn't care about her any more than they care about us.</P>
<P>It's about making money. Management is making plenty of money, but we're not. The job's very stressful. I'm a driver, and we have to deal with thousands of personalities everyday.</P>
<P>The mechanics have to make the buses function. One day they may just be changing a flat, but the next day, they're rebuilding a motor.</P>
<P>And the bus has to roll, and there's always a strict time limit. You're under the gun every minute. Management should trade places with us and see how it goes. They'd sit down and cry.</P>

<B><P>Joe</B> <BR>
I went out three times to walk [the grocery strike picket line] with the grocery workers in my neighborhood. I've been stopping by late at night to see if they need anything. A victory for us will be another win for all working people--people who aren't even in unions, and who could benefit from being in unions.</P>
<P>Corporate America has a movement underway to undercut all these things that have taken so many years to establish: benefits, wages, all those things. This strike shows how they're trying to whittle it away. They want to increase their bottom line and turn us back to the way it was before unions.</P>
<P>Why are the unions in Europe so strong and the unions here in this country so weak, comparatively? I look at these two strikes, and I see people crossing the picket lines.</P>
<P>We've had a few people cross over--some lower management that just established their own union, maybe two, three years ago, and they've crossed over. And I don't think the strikes are embracing the community in a huge way.</P>
<P>I mean, we all work. Everybody I see driving by me now are working people. All we know are each other. I don't think we're going home to neighborhoods, meals and cocktail parties with the Donald Trumps and the Rockefellers and those people.</P>
<P>We're just interacting with working people. But for some reason, being a working person is a dirty word. We've got to turn that around in a way that I can't even imagine.</P>
<P>The most important thing we need to do is to educate ourselves about past struggles. From there, we can organize. From organization we can move into action, from action we can have concrete results and then maintain those results with more education. It's a cycle of events that need to happen. </P>

