<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">What Washington means by the FTAA...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Free Trade Assault on the Americas</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 7, 2003 | Page 3 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">A NEW U.S. invasion of foreign territory will be launched this month--not in the Middle East, but from Miami. This time, Washington's offensive will take the form of the Free Trade Area of the Americas (FTAA), a NAFTA-style agreement aimed at giving the U.S. greater economic control over 33 countries in the Western Hemisphere, stretching from the Arctic Circle in Canada to the southernmost tip of South America.</P>
<P>At a meeting of trade ministers on November 19 and 20, U.S. officials will try to accelerate negotiations for a deal that, like NAFTA, would allow corporations to sue in order to overturn local laws--environmental safeguards or measures prohibiting the privatization of government services, for example.</P>
<P>Technically, the FTAA would treat all countries equally. But does anyone really believe that a company from a desperately poor country like Paraguay would compete on equal footing with Microsoft or General Electric?</P>
<P>The built-in U.S. domination of the FTAA is provoking opposition to the treaty throughout Latin America. In Miami, government leaders will face big protests--just as they did when the FTAA campaign was kicked off at the Summit of the Americas in Quebec City in 2001. Unions, environmental activists, students, peasant groups and more are planning on turning out.</P>
<P>The AFL-CIO organizer for the labor demonstrations, Deborah Dion, told <I>Socialist Worker </I>that tens of thousands of union members are expected to protest--1,500 members of the United Steelworkers of America, two dozen busloads of retirees from South Florida, delegations from the electricians', machinists' and teachers' unions, and more. "This is George W. Bush's free-trade deal, and it's the wrong message for working people," she said. "We think we can beat this if we show strength in numbers here and hold members of Congress accountable."</P>
<P>The largest event will be a permitted march November 20, but a number of groups are planning direct action as well. United for Peace and Justice has sought to link the antiwar movement to the Miami protests, calling for nonviolent civil disobedience to disrupt FTAA negotiations.</P>
<P>Protesters in Miami will be inspired by the recent uprising in Bolivia, where a mass rebellion drove the hated U.S.-backed president, Gonzola S&aacute;nchez de Lozada, from office over his plans to privatize and export the country's natural gas. That victory was the latest in a series of popular protests against the free-market agenda in Latin America--including an earlier uprising in Bolivia and the ouster of Argentina's government in 2001. </P>
<P>According to a recent survey by the Latinobarometro group, only 16 percent said they were fully satisfied with the market economy as a model. This sentiment has fueled the rise of elected leaders who've taken a tougher line with Washington--including Hugo Ch&aacute;vez in Venezuela, N&eacute;stor Kirchner in Argentina and Luis In&aacute;cio "Lula" da Silva in Brazil. </P>
<P>Brazil joined with other developing countries, including India and China, walking out of September's summit of the World Trade Organization (WTO) in Canc&uacute;n, Mexico--to protest continued agricultural subsidies in the U.S. and European Union, despite promises to reduce or abolish them.</P>
<P>The Canc&uacute;n collapse was the latest in a series of setbacks for the free-market ideologues since the 1999 WTO summit in Seattle, which also unraveled after some developing countries refused to bow to big-country pressure. But when it comes to the FTAA, the U.S. can't wait. </P>
<P>Washington wants to turn the Americas into a regional trade zone to bolster U.S. corporations against their European and Asian rivals. Alongside the FTAA comes an increased U.S. military presence in Latin America--with the "war on drugs" serving as a cover for Washington's intervention in Colombia's civil war, for example.</P>
<P>In Miami, the U.S. aims to regain the momentum for the so-called "Washington consensus" of free trade, privatization and "flexible" labor policies--that is, attacks on unions.</P>
<P>But opponents of the FTAA have an opportunity in Miami, too. We can expose how U.S. economic and military policies are part of the same imperialist power grab by Washington--and build solidarity across borders to turn back the free-trade attack.</P>



