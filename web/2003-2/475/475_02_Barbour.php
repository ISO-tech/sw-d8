<P><font face="Times New Roman, Times, serif" size="4">Haley Barbour runs for Mississippi governor</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>He's proud to be a bigot</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 7, 2003 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">"HE NEVER forgot his roots." That's what George W. Bush had to say about Haley Barbour at a fundraiser for the Republican lobbyist, who is running for governor of Mississippi.</P>
<P>For once, Bush was telling the truth--and the evidence exists to prove it. A photo of Barbour is featured on the Web site of the Council of Conservative Citizens (CCC)--the racist, anti-Semitic and homophobic group that traces its ancestry back to racist white "citizens councils" that organized the attack on civil rights activists during the 1960s.</P>
<P>The CCC's home page--which also featured links to such articles as "In Defense of Racism"--includes a photo of Barbour mugging it up at a CCC-sponsored barbecue with the group's field director, Bill Lord. When asked whether he would demand to have his photo removed from the site, Barbour said no.</P>
<P>According to opinion polls, the former head of the Republican National Committee and star GOP fundraiser was running neck and neck in his campaign against Democratic incumbent Gov. Ronnie Musgrove as <I>Socialist Worker </I>went to press. The cornerstone of Barbour's campaign has been opposition to affirmative action and social spending for the poor.</P>
<P>Barbour also makes sure that he wears his pin of the Mississippi state flag, which still features a large Confederate flag in its design, even after attempts to have the racist symbol removed two years ago. It's clear whose vote Barbour is aiming for. He's hoping to solidify Southern support for the Republican Party--and he's playing the race card to do it.</P>
<P>And this bigot has the Bush administration's full support.</P>
<P>Among the big-name Republicans who showed up in Mississippi to support their favorite right-wing lobbyist are Dick Cheney, Rudolph Giuliani, Bob and Elizabeth Dole, former White House press secretary Ari Fleischer, and Dubya's little brother, Florida Gov. Jeb Bush.</P>
<P>Bush made two visits to stump for Barbour, one just days before Mississippi voters were scheduled to go to the polls on November 4. We have to stop this racist good ol' boy--and all the good ol' boys in Washington who are backing him to the hilt.</P>



