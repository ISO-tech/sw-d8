<P><font face="Times New Roman, Times, serif" size="4">White House has the gall to label critics as "racist"</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Occupation isn't freedom</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 7, 2003 | Page 4 </font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Condoleezza Rice has often claimed the U.S. occupation of Iraq as a legacy of the struggles of Dr. Martin Luther King and the quest for "freedom." She accuses critics of being racist for supposedly thinking that Iraq couldn't live under "Western democracy." The hollowness of this argument was revealed recently when Rice refused to condemn the utterly racist statements of Army Lt. Gen. William G. Boykin, the deputy undersecretary of defense for intelligence.</P>
<P>Boykin--in and out of uniform, in churches and elsewhere--has described Bush's war on terror as the battle of a "Christian Nation against Satan." He said of his battle against Osman Otto, a Muslim warlord in Somalia, "I knew that my God was bigger than his. I knew that my God was a real God, and his was an idol." </P>
<P>After Otto's capture, Boykin has said that he told Otto, "You underestimated our God." Rice's covering for this vile rhetoric reveals that what the U.S. is doing in Iraq is not about "freedom," but about occupation. </P>
<P>That is why people who want to walk in the footsteps of Dr. King should not support a war King would have fought with his last breath. Instead, they should put on their protest shoes and march against this occupation. </P>
<P>Among the public at large, more people than ever question Bush. We saw this on October 25, as tens of thousands of us in Washington, D.C., and San Francisco gave a concrete expression to these doubts--and took important new steps in organizing an ongoing opposition to Washington's occupation of Iraq and its plans for future conquests.</P>

<B><P>David Zirin, </B>Prince George's County, Md.</P>


