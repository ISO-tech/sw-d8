<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The facts about the sexist backlash</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 7, 2003 | Page 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"WHY DON'T women run the world? Maybe it's because they don't want to." Thus reads the latest installment in the ongoing media backlash against the women's movement--this one the October 26 cover story of the <I>New York Times Magazine, </I>"The Opt-Out Revolution," by Lisa Belkin.</P>
<P>The article features a small group of affluent women who abandoned careers for full-time motherhood --the only empirical support Belkin offers for her sweeping conclusion that women prefer the rewards of nurturing to "standing at the helm in the macho realms of business and government and law." This handful of contented stay-at-home moms notwithstanding, the overarching trend is more, not fewer, married women in the workforce than ever before.</P>
<P>Because of discrimination, however, women workers' wages, dollar for dollar, amount to only 73 percent of men's--and when measured by annual earnings (including those of part-time workers), women earn just 59 cents for every dollar earned by men. Working mothers face child care costs up to $10,000 per year per child, while averaging 80 hours per week between paid labor and unpaid household chores, according to financial writer Ann Crittenden.</P>
<P>Gender discrimination, not personal choice, prevents women from sharing in running the world. But then, as author Susan Faludi documented in her 1991 book, <I>Backlash, </I>the mass media has never allowed the facts to get in the way of taking pot shots at the women's movement.</P>
<P>A 1986 <I>Newsweek </I>magazine cover story claimed that "a woman over 40 has more of a chance of being shot by a terrorist than getting married." Faludi showed that the Harvard study on which <I>Newsweek </I>based its "findings" did not exist. But <I>Newsweek </I>never retracted its claim, and the moral of the story remained--that women who choose to pursue careers would wake up at age 40, panic-stricken, infertile and alone.</P>
<P>The backlash against women surged forward on another front last month, when Arnold Schwarzenegger sailed to victory in the California gubernatorial election, despite admitting that he had "behaved badly," sexually harassing women over a span of decades. Comedian Jay Leno joked dismissively, "This proves he would be a hands-on governor."</P>
<P>TV shows starring men behaving badly claim, as does <I>The Man Show,</I> that they are tongue in cheek--as if only the utterly humorless could be offended by the sight of scantily-clad women lap dancing on leering, belching men. <I>The Man Show</I> Web site describes the show as "a place where men can come together, look at the cans on this chick named Heather, Juggy girls on trampolines, time to loosen those blue jeans." (Are you laughing yet?)</P>
<P>But sexual harassment is no laughing matter for the millions of women who experience it--as 21 percent of women do in their workplaces, shown in a 2002 Employment Law Alliance survey. One out of every six women will be raped in her lifetime, and domestic violence accounts for over 200,000 trips to the emergency room each year, according to the Domestic Violence Information Center.</P>
<P>The backlash against women is poised to ratchet up yet another notch when George Bush makes good on his pledge to sign a congressional ban on the intact dilation and extraction abortion procedure, widely known as "partial birth abortion"--the anti-choice movement's deceptive label adopted by the mass media. In reality, this ban is an enormous anti-choice publicity stunt, designed to rob women of control over their reproductive lives.</P>
<P>It gives the false impression that women choose late-term abortions for frivolous reasons, when third trimester abortions are performed only under the most traumatic circumstances--to save the life or health of a woman, or if the fetus is severely deformed. The procedure is extremely rare (fewer than .05 percent of all abortions) and is the most medically appropriate procedure for some women in the second or third trimester.</P>
<P>Banning this medically necessary procedure will endanger those women's lives or health--yet the ban passed through Congress without even a clause to protect the health of the woman. Once again, the facts didn't get in the way.</P>


