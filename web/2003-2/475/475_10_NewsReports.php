<P><font face="Times New Roman, Times, serif" size="5"><b>News and reports</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 7, 2003 | Page 10 </font></P>

<P><p><font face="Times New Roman, Times, serif" size="4"><b>No to the occupation of Iraq</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By David Thurston, Michael Hardin and Erik Wallenberg</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">New York City</B> <BR>
Antiwar activists at City College of New York (CCNY) are gearing up to protest Secretary of State Colin Powell's visit to campus November 10. Powell is scheduled to give a major foreign policy address, and activists are discussing how to mobilize, including New York City Labor Against War, United for Peace and Justice and CCNY student organizations.</P>
<P>"At the beginning of [Powell's] career, he dismissed reports of a massacre at My Lai," Vietnam veteran Ben Chitty told <I>Socialist Worker.</I>"At the end, he presided over the concealment of disabling and sometimes fatal injuries to almost 200,000 men and women under his command in the first Gulf War. Now he puts the same talent at the service of a buffoon masquerading as president, and again, other peoples' children die."</P>

<B><P>San Antonio, Texas</B> <BR>
Five hundred people came out to protest President Bush at a reelection fundraiser October 30. Opposition to Bush's policies in Iraq, as well as anger in the face of the Bush's one-sided class war, was a major component of the rally.</P>

<B><P>Montpelier, Vt.</B> <BR>
More than 500 people gathered October 25<SUP> </SUP>at the Vermont state capital building to protest the continuing occupation of Iraq and to stand in solidarity with activists at the national anti-occupation demonstration in Washington, D.C. "Hey Bush we know you, you're a liar and a killer too!" the crowd chanted, as hundreds lined the street to wave to the marchers.</P>


