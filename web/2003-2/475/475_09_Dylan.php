<P><font face="Times New Roman, Times, serif" size="4">Exploring how politics shaped his music</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Dylan's chimes of freedom</b></font></P>

<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Anthony Arnove</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 7, 2003 | Page 9 </font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">Mike Marqusee, <B><I>Chimes of Freedom: The Politics of Bob Dylan's Art.</I> </B>New Press, 2003, 336 pages, $24.95.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">MORE NONSENSE has been written about Bob Dylan than perhaps any other popular artist in the United States. Most of what passes as Dylan commentary, especially these days, is premised on either a denial of the politics of his music or relief that he largely stopped writing what he once famously called "finger-pointin' songs."</P>
<P>In his brilliant new book <I>Chimes of Freedom,</I> Mike Marqusee puts the vast body of Dylan "scholarship" to shame, offering readers an engaging and masterful study of the political currents that shaped Dylan's art and which, in turn, his remarkable music shaped. Much like he did in his excellent book on Muhammad Ali, <I>Redemption Song,</I> Marqusee here seeks to understand Dylan's music within the ebbs and flows of the massive social upheavals and struggles of the civil rights movement and broader social movements of the 1960s.</P>
<P>Marqusee, who left the U.S. in the 1970s for England, where he has been an antiwar and antiracist activist for years, focuses on Dylan's trajectory through the 1960s, a period in which he was not only remarkably productive, but radically changed popular music. <I>Chimes of Freedom</I> covers a period in which Dylan wrote some of the most damning, thought-provoking and lyrical songs anybody had ever put to paper, including "Only a Pawn in Their Game," "Chimes of Freedom," "When the Ship Comes In," "The Lonesome Death of Hattie Carroll" and "Masters of War."</P>
<P>Listen to "Masters of War" today, and the lyrics and anger cut though all the lies our rulers tell about Iraq: "You fasten the triggers/For the others to fire/Then you set back and watch/When the death count gets higher/You hide in your mansion/As young people's blood/Flows out of their bodies/And is buried in the mud."</P>
<P>Dylan ends the song unapologetically: "And I hope that you die/And your death'll come soon/I will follow your casket/In the pale afternoon/And I'll watch while you're lowered/Down to your deathbed/And I'll stand o'er your grave/'Til I'm sure that you're dead."</P>
<P>When Dylan returned to his interest in rock 'n' roll--notably in songs such as <A NAME="OLE_LINK1">"Like a Rolling Stone" </A>and "Maggie's Farm"--folk purists claimed he had "sold out." But Marqusee shows how Dylan helped break folk music out of its narrow confines to bring his music and ideas to a much wider audience.</P>
<P>"The first time I heard Bob Dylan," Bruce Springsteen later remembered, "I was in a car with my mother listening to WMCA, and on came that snare shot that sounded like somebody's kicked open the door to your mind."</P>
<P>Through rock, Dylan also reached an audience that was being radicalized by the movement against the Vietnam War and by the Black Power movement. Marqusee recounts the story of Huey Newton, Eldridge Cleaver and Bobby Seale cranking out Dylan's sneering "Ballad of a Thin Man" while editing the Black Panther Party's newspaper, <I>The Black Panther. </I>"These brothers would get halfway high, loaded on something, and they would sit down and play this record over and over and over," Seale recalled.</P>
<P>As Marqusee notes, "The Panthers were a political response to many of the same tides that shaped Dylan's artistic arc: the successes and frustrations of the civil rights movement," as well as "the bankruptcy of Vietnam War liberalism." As Marqusee shows, part of that response was an emphasis on individualism and personal consciousness, rather than struggle and organization, which Dylan's music increasingly reflected (and in some ways presaged) as the decade continued.</P>
<P>Marqusee deals honestly with the many contradictions of Dylan's art and politics, without trying to simplify them or wish them away. And he reminds us why, after more than 40 years of recording and performing, Dylan, though all his ups and downs, still kicks open the doors of our minds.</P>


