<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The Democrats duck another fight</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 7, 2003 | Page 3 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">EVERY WEEK brings new and more horrific evidence of what a disaster the U.S. occupation of Iraq has become. And all President Moron can do is repeat memorized catchphrases about the progress being made in Iraq. So how is it that Democrats in Congress managed to get beaten by the White House once again this week--this time, over legislation to give the Pentagon another $87 billion for the occupation of Iraq and Afghanistan next year?</P>
<P>Despite growing questioning of the occupation reflected in every opinion poll, Democratic leaders staked their fight on trying to get $10 billion of the total package turned into loans--payable by Iraq. This wasn't a challenge to the Bush administration so much as a cruel insult to the Iraqi people, crushed by a decade and a half of U.S. military and economic war.</P>
<P>And <I>still </I>the Democrats lost. The loan provision tacked on as an amendment in the Senate was stripped out of the final version of the legislation. Among the nine Democrats trying to win the party's presidential nomination, there's plenty of anti-Bush talk on the campaign trail.</P>
<P>But behind the rhetoric, it's a different story. Even Howard Dean--who became a favorite of many Democrats because of his willingness to oppose Bush's invasion of Iraq--supports a continued U.S. occupation, like all the other major candidates. "Now that we're there, we can't pull out responsibly," he said at a candidates' forum last month.</P>
<P>For Dean--as for the rest of the party establishment--the differences with the Bush White House over foreign policy, when they exist at all, are about tactics, not principles. There are candidates for the Democratic nomination--Dennis Kucinich, Carol Moseley Braun and Rev. Al Sharpton--who oppose Bush's occupation.</P>
<P>For this, these long shots are regularly harangued by party leaders--and not a few well-known liberal voices to boot--for playing into the Republicans' hands by being too strident. They are tolerated by the Democratic establishment only because they can be trusted to deliver their supporters to whoever the eventual nominee is, no matter how conservative.</P>
<P>The truth is that the Democrats are not a party of "peace." Their long and grim record in office shows that they are the other mainstream war party--counted on by America's rulers to uphold U.S. economic and military interests overseas.</P>
<P>Supporting the Democrats as the "lesser evil" to Bush neglects the necessary task of building a real alternative to the war policies of both parties--in the here and now. This isn't an abstract question.</P>
<P>When, for example, activists at Columbia University in New York City contacted a Students for Dean group on campus last month to discuss organizing for the October 25 antiwar demonstration in Washington, D.C., they were told that Dean's campaign was encouraging supporters not to go--and instead travel to New Hampshire, site of the first presidential primary, to knock on doors.</P>
<P>There could be no better example of what's wrong with supporting an "antiwar" Democrat--and why we need to commit ourselves to organizing the kind of social movement that will put pressure on all of Washington's war makers.</P>


