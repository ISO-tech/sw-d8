<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MAXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Has Iraq become the new Vietnam?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 7, 2003 | Page 9 </font></P>

<P> <font face="Times New Roman, Times, serif" size="3">"THE MILITARY task the United States confronted in Southeast Asia was far more formidable than the one we face today in Iraq," Michael Mandelbaum, senior fellow at the Council on Foreign Relations, recently wrote. This might seem a comforting thought to U.S. military planners at first glance. </P>
<P>The U.S. armed force did face a far larger guerrilla insurgency in Vietnam in the 1960s. American death rates were on average much higher in Vietnam than they are now. U.S. deaths in Iraq are hovering around one per day, whereas U.S. death rates in Vietnam peaked around the sixth year of the war at over 2,000 a month.</P>
<P>It should be kept in mind, however, that it took years for casualties to escalate to this level. Moreover, the rate at which U.S. soldiers are being wounded in Iraq is about 360 per month--a fact that the Pentagon and the press are underplaying.</P>
<P>Vietnam was one of the last colonial wars. After the French colonialists were defeated and forced to leave at the hands of the nationalist Vietminh, the U.S. intervened, created a puppet regime in the south and prevented popular elections meant to reunite the country from happening.</P>
<P>The U.S. presence in Vietnam began small, with a few thousand military "advisers" to the newly created Army of South Vietnam. By the late 1960s, there were over 500,000 U.S. troops in Vietnam.</P>
<P>"The Vietnamese Communists recognized that they could not defeat the military forces of the United States," says Mandelbaum, "but they achieved an American withdrawal by raising the costs of staying, in lives and money, beyond what the American public was willing to tolerate."</P>
<P>This is accurate if you include in the term "public" U.S. soldiers. One of the most important factors in forcing a U.S withdrawal in Vietnam was the complete breakdown of morale among the troops, leading to sullen refusal and even open rebellion.</P>
<P>The irony is that whereas during Vietnam the U.S. government justified sending U.S. troops because their Vietnamese proxy army was failing to defeat the guerrilla movement, today the reverse argument is made: U.S. troops need to be replaced by U.S.-trained Iraqi forces to front for the occupiers. The historical order is reversed, but the outcome could easily be the same, even before things escalate to the level they did in Vietnam.</P>
<P>What's remarkable about the current period is how much quickly a crisis has developed for the Bush administration over this "quick victory," and at a far lower level of casualties or combat intensity.</P>
<P>The growing credibility gap between Bush and the "public" isn't just driven by the absolute numbers of casualties.</P>
<P>There is a palpable feeling among troops that they're being "jerked around"--over combat pay, the length of their tours of duty, food and health care and over the reasons the war was conducted in the first place. Half of the troops polled by <I>Stars and Stripes</I> magazine say that they will not re-enlist.</P>
<P>Underlying these developments is not just the Iraqi resistance, but also the question of the economy at home. Joblessness and drastic social spending cuts alongside Bush's corporate handouts is enraging many working-class Americans. Far more than during Vietnam, this war steals butter to pay for guns. </P>
<P>True, the stakes are enormous for the U.S. in Iraq--the "credibility" of U.S. imperialism is at stake. It will therefore take far greater resistance at home and abroad to get the U.S. to quit Iraq. But the U.S. faces a severe problem with any attempt at long-term occupation.</P>
<P>Colonialism has been discredited for decades. Gone are the days when an occupying power could intimidate a people or instill in them a sense of inferiority. The U.S. therefore can only rule Iraq by naked force, and naked force provokes resistance, which in turn brings on more naked force. Under these circumstances, the Iraqi resistance will grow, U.S. troop discontent will grow--and Bush's crisis will grow.</P>



