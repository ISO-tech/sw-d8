<p><a name="USLAW"></a><font face="Times New Roman, Times, serif" size="4"><b>U.S. Labor Against the War</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar, USLAW delegate</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--More than 150 people attended U.S. Labor Against the War (USLAW) meeting October 25 to organize for an end to the U.S. occupation of Iraq. Representatives from 99 labor organizations attended, including officials, staff and members from a variety of local and international unions as well as ad hoc labor antiwar committees.</P>
<P>The largest contingents included teachers and members of the Service Employees International Union. A highlight of the event was an eyewitness report from a recent labor delegation to Iraq by journalist David Bacon and Clarence Thomas, former secretary-treasurer of the International Longshore and Warehouse Union.</P>
<P>In a mission statement adopted at the meeting, USLAW called for an immediate withdrawal of U.S. troops, staking out a firm position in the debate taking place in the antiwar movement. There was controversy over the 2004 elections, although for legal reasons, USLAW can't endorse any candidates.</P>
<P>USLAW co-convenor Bob Muehlenkamp--who also serves as a labor adviser to Democratic presidential candidate Howard Dean--used his presentation to call for "regime change" in the U.S. in next year's vote--a formulation that was challenged by a number of delegates. Muehlenkamp also called on USLAW members to challenge the AFL-CIO's official silence--not only on Iraq, but on all foreign policy issues.</P>
<P>This apparently doesn't extend to Israel and Palestine, however. The conference lacked even a discussion of the Palestinian issue for educational purposes, let alone policy. Those shortcomings notwithstanding, USLAW emerged from the weekend better equipped to carry out antiwar and anti-occupation organizing in the unions. </P>

