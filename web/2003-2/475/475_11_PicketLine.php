<P><p><font face="Times New Roman, Times, serif" size="4"><b>Teamsters for a Democratic Union</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Danny Katch</b></font> </P>

<P><font face="Times New Roman, Times, serif" size="3">DETROIT--More than 200 Teamster members and officials met October 24-26 at the annual convention of Teamsters for a Democratic Union (TDU). TDU is the largest and longest-running organization of rank-and-file militants in the American labor movement.  </P>
<P>The convention highlighted some of the successful organizing of the past year. Jona Fleurimont, a school bus driver in Teamsters Local 854 in New York City, spoke about how he and dozens of other drivers have stood up to violent threats from both union and company officials to elect their own shop steward and take control of their union.  </P>
<P>Other Teamsters reported on the fight against the cutbacks that Teamster president James Hoffa Jr. is overseeing in the Central States and Western funds. TDU has organized meetings and committees to fight these attacks, drawing hundreds in places like North Carolina.  </P>
<P>The convention also reflected the serious setbacks TDU has suffered under the employers' offensive and Hoffa's weak resistance. Northwest flight attendants in Local 2000 were a longstanding base of TDU support, but earlier this year they regrettably voted to decertify from the Teamsters after years of negligence and harassment from the International.  </P>
<P>Other reformers have been victimized or isolated. Hoffa invokes patriotism and the "war on terrorism" to justify the union's collaboration with Bush and employers.   </P>
<P>But the convention continued its practice of not addressing political questions such as the war in Iraq or the 2004 elections. The atmosphere was sober, but the convention was marked by a determination to continue the long struggle to push the union in a more confrontational direction. To that end, TDU announced plans to launch a series of leaflets and a Web site to aid UPS shop stewards to help organize on the shop floor.  </P>

