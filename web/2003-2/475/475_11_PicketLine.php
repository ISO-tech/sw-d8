<p><a name="UV"></a><font face="Times New Roman, Times, serif" size="4"><b>University of Vermont</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Erik Wallenberg</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">BURLINGTON, Vt.--After working without a contract for three-and-a-half months, service and maintenance workers at the University of Vermont (UVM) are taking action. United Employees (UE) Local 267 organized a November 1 rally and march on the UVM president's house. </P>
<P>Nearly 150 people formed three separate marches--one with community members, one with unionized faculty and unorganized staff, and one with students--and then converged on the president's house in order to demand a fair contract.</P>
<P>"The president doesn't actually live in this residence, but is given a $22,000 a year stipend to live in a house he built on Lake Champlain in an adjoining suburb," said UE organizer Heather Reimer. "Meanwhile many of our workers don't even make $22,000 a year to support entire families."</P>
<P>Lester, an electrical worker, said he was at the rally because "they can pay a livable wage--and they should." Workers are also demanding an end to the practice of firing injured workers who can't return to full duty within six months of their injury.</P>
<P>Union president Carmyn Stanko spoke of the need to "give voice to those who can't come out here. "Many don't speak English, they aren't confident about what their rights are, and they are in powerless positions. We're here to give them a voice. It's energizing to see the community support. It fills your tank to do even more."</P>


