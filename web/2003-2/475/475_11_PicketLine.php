<p><a name="Restaurant"></a><font face="Times New Roman, Times, serif" size="4"><b>New York City restaurants</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Chanting "No contract! No work!" and "!S&iacute;, se puede!" more than 400 waiters, dishwashers and bartenders held a fiery march October 28 outside Four Seasons, 21 Club and other well-known restaurants. As the October 31 contract deadline came and went, restaurant workers in Hotel Employees and Restaurant Employees (HERE) Local 100 were still at work--despite a strike authorization vote last weekend of more than 400 to 22.</P>
<P>As <I>Socialist Worker </I>went to press, it still wasn't clear whether workers would strike, shutting down the city's ritziest restaurants. The restaurant companies are looking to double co-payments to $20 and cut the maximum yearly coverage in half to $500,000.  </P>
<P>But workers aren't backing down. "I don't want to be out here like this," said one waiter who has worked at Four Seasons for 27 years. "But we have no other choice." That spirit of defiance was also evident at the rally as workers, many of them immigrants who toil behind the kitchen doors, angrily chanted at the front entrances of their posh employers.</P>

