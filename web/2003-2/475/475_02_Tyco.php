<P><font face="Times New Roman, Times, serif" size="4">Former Tyco bosses on trial for corporate crimes</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The $2 million birthday party</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 7, 2003 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">A FEW centuries ago, rich men hired painters to produce portraits of themselves, surrounded by symbols of their wealth--their homes, their land and their property. As the world discovered last week during the trial of Dennis Kozlowski, the crooked former CEO of Tyco, the obscenely rich now use videotape.</P>
<P>Kozlowski and codefendant Mark Swartz--Tyco's former chief financial officer--face charges of grand larceny, conspiracy, securities fraud and enterprise corruption for stealing about $600 million from their own company. If convicted, they face up to 30 years in prison.</P>
<P>As part of the trial, jurors were shown an edited version of a four-hour videotape shot at a lavish party that Kozlowski threw for his wife's 40th birthday party in 2001. The weeklong party on the Italian island of Sardinia cost $2 million--most of it on the company's tab.</P>
<P>It featured a birthday cake in the shape of a naked woman with sparklers on her breasts, an anatomically correct ice sculpture of David that dispensed vodka through its penis and scantily clad male and female models posing with guests for poolside pictures. "It's going to be a fun week," Kozlowski told guests as they arrived. "Eating, drinking, whatever. All the things we're best known for."</P>
<P>And theft. Turns out Tyco footed most of the bill. Many guests had their plane tickets paid for. And for the tidy sum of $250,000, Jimmy Buffet and his band were flown in to perform.</P>
<P>The video may provide a particularly graphic illustration of the greed and decadence that Kozlowski and Co. delighted in, but it's only the tip of the iceberg. Kozlowski raked in far greater sums than the $2 million blown on the party.</P>
<P>According to prosecutors, Kozlowski and Swartz stole $170 million in unauthorized compensation by plundering loan programs operated by Tyco for its employees--and giving themselves pay and bonuses that weren't approved by the board. What's more, Kozlowski and Swartz fleeced the company and its shareholders for a combined $430 million by disguising the poor financial health of the company between 1995 and 2002 in order to sell their company stock at inflated prices. </P>
<P>Using the same methods as Enron to hide debt and pump up share prices, Kozlowski oversaw thousands of acquisitions and mergers to transform Tyco from a mid-sized New Hampshire manufacturer into a Bermuda-based multinational with 250,000 workers. In the process, Tyco piled up an incredible $27 billion in debt--which executives hid from the public. </P>
<P>When it all came crashing down, shareholders, workers' retirement funds and low-level employees took the hit. Kozlowski had already looted the company--and his take was safely squirreled away.</P>


