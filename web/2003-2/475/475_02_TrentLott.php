<P><font face="Times New Roman, Times, serif" size="5"><b>Lott's "insane" advice on Iraq occupation</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 7, 2003 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">WITH ATTACKS on U.S. occupation forces on the rise in Iraq, even congressional Republicans are voicing their concerns. Sen. Trent Lott (R-Miss.) offered up his own sick solution.</P>
<P>"Honestly, it's a little tougher than I thought it was going to be," Lott said earlier this week, in the Washington newsletter <I>The Hill</I>. "If we have to, we just mow the whole place down, see what happens. You're dealing with insane suicide bombers who are killing our people, and we need to be very aggressive in taking them out."</P>
<P>Who's calling who "insane"? And to judge from reports of what the U.S. occupation looks like--from U.S. troops indiscriminately firing on Iraqi civilians, to the daily humiliation of searches and interrogations of people who still live without clean water and electricity--"mow them down" doesn't seem far from what the Bush administration has planned.</P>
<P>One thing's almost certain--it's a recipe for more opposition from Iraqis who are rightfully fed up with the U.S. government's brutal rule.</P>

