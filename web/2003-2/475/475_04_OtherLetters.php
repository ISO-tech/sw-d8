<P><p><font face="Times New Roman, Times, serif" size="4"><b>The "saint" who covered for tyrants and crooks</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</I>,</B> <BR>
The Vatican recently celebrated the 25th anniversary of the papacy of John Paul II with great fanfare. The celebrations included the Pope declaring the late Mother Teresa "blessed," ensuring that in the near future, she will be declared a saint.</P>
<P> Mother Teresa, who died in 1998, was considered a "living saint" for her work among the poorest of the poor in Calcutta, India. It was a reputation that she did not deserve.</P>
<P> She spent her life taking the confessions of and "forgiving" tyrants such as the Duvalier family of Haiti, who ruled through terror and dictatorship. Mother Teresa espoused an extreme Catholic fundamentalism that made the lives of the poor worse--especially for women--by her opposition to birth control and abortion. She even traveled to Ireland in the mid-1990s to campaign against efforts to lift the constitutional ban on divorce!</P>
<P> Mother Teresa also had no problem taking stolen money from the poor and elderly. Charles Keating, one of the infamous savings and loan crooks from the 1980s, gave her a $1 million contribution from money he swindled. When contacted about returning the money by the Los Angeles County Prosecutor's Office, she didn't return the call.</P>
<P>The Catholic Church has fallen on hard times, especially in the U.S, and the Pope's campaign to turn Mother Teresa into a saint is obviously a public relations effort to turn the image of the Church around. Yet the life of Mother Teresa shows how difficult it is to get any charity for the poor and oppressed from such Christians.</P>
<B><P>Joe Allen, </B>Chicago</P>

<p><font face="Times New Roman, Times, serif" size="4"><b>A tribute that did Said justice</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Shane Dillingham's suggestion that the <I>Socialist Worker</I> obituary for Edward Said (<I>SW,</I> October 24) failed "to make the distinction between Zionist politicians, who may be Jewish or not, and the Jewish community as a whole" is off the mark. The statement Dillingham objected to from Al Jazeera that Said was attacked by the "Jewish establishment" in the U.S. is hardly an example of anti-Semitism, which <I>SW</I> has consistently opposed.</P>
<P>Said was the target of vitriolic attacks by organizations such as Hillel and the American Jewish Congress, which define themselves as Jewish, rather than Zionist, organizations. It was not merely Zionist politicians, but religious Jewish leaders who despicably targeted Said.</P>
<P>We make no concession to anti-Semitism by acknowledging this fact. As Gasper's obituary made clear, Said believed, "The only alternative was a single democratic state with equal rights for all its citizens, Jews and Arabs." We won't get closer to achieving this goal by looking for anti-Semitism where it doesn't exist.</P>

<B><P>Anthony Arnove, </B>New York City</P>


