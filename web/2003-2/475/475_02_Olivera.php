<P><font face="Times New Roman, Times, serif" size="5"><b>A victory for Oscar Olivera</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 7, 2003 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">"&#161;UN ABRAZO fuerte desde Bolivia!"</P>
<P>"A hearty embrace from Bolivia!" wrote Oscar Olivera, a leader of the struggle for social justice in Bolivia, to his supporters around the world last week. Today, the executives of Manaco [a shoe company owned by Bata, a Canadian multinational] called me to tell me that they are returning my wages and other benefits."</P>
<P>Manaco executives had tried to fire Oscar for his vigorous support of 16 laid-off coworkers&#150;and his criticism of the company's decision to impose a week of factory downtime without pay. But after being flooded by e-mails and letters of support from around the world, Manaco backed down.</P>
<P>"I want to say thanks," wrote Oscar, "but also thanks on behalf of my family for your solidarity." Oscar's victory came just weeks after the nationwide rebellion of Bolivia's workers and peasants, who took to the streets in mid-October to drive out their U.S.-backed president. These struggles in Bolivia have set an inspiring example for those fighting for justice around the world.</P>


