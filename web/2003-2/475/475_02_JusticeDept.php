<P><font face="Times New Roman, Times, serif" size="5"><b>Ashcroft censors a critical report</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 7, 2003 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">ATTORNEY GENERAL John Ashcroft and his Justice Department have some dirty laundry to hide. And they aren't above censoring a critical report to keep it tucked out of sight.</P>
<P>In June 2002, the Justice Department underwent a voluntary audit by the consulting group KPMG. KPMG's report uncovered serious flaws with the department's hiring, promotion and retention of minorities. "The department does face significant diversity issues," the report concluded. "Whites and minorities, as well as men and women, perceive differences in many aspects of the work climate. For example, minorities are significantly more likely than whites to cite stereotyping, harassment and racial tension as characteristics of the work climate."</P>
<P>According to KPMG, while minorities account for 15 percent of the department's attorneys, they make up just 7 percent of career management-level attorneys and 11 percent of supervisory assistant U.S. attorneys. But these findings never made it into the "public" report.</P>
<P>That's because when the report was finally posted to the Justice Department's Web site two weeks ago, it was edited so heavily that half of its 186 pages were blacked out. According to the Associated Press, whoever edited the report chose to delete most of the conclusions and recommendations. In other words, the incriminating stuff.</P>
<P>The deleted passages were recovered thanks to Russ Kick, a self-described "information archeologist" who electronically stripped out the black lines hiding the full text. "The Justice Department has sought to hide from the public statistically significant findings of discrimination against minorities within its ranks," said David Shaffer, a lawyer who has represented employees of federal agencies in class-action discrimination suits, told the <I>New York Times</I>.</P>
<P>Still, the Justice Department defends its censorship. "This was a study that we commissioned of our own volition to get a look at what our work force looked like," snipped Stacey Plaskett Duffy, a senior counsel to the deputy attorney general. "We didn't have to let people know we were doing this."</P>



