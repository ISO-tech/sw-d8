<P><font face="Times New Roman, Times, serif" size="4">Bush wants to protect Colombian pipeline</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Putting oil profits first</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 11, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE COVER story for Washington's supposed war on drugs in Colombia was exposed as a lie once again last month. According to press reports, the Bush administration is requesting up to $147 million in the 2004 budget--not to battle Colombia's narcotics trade, but to protect a private oil pipeline.</P>
<P>The money would go to beef up protection of the Cano Limon-Covenas oil pipeline, which runs through the northeastern province of Arauca. Details of the proposal are included in a report released last month by the Washington Office on Latin America, titled "Protecting the Pipeline: The U.S. Military Mission Expands."</P>
<P>The pipeline is jointly operated by the Colombian state oil company Ecopetrol and the U.S. oil company Occidental Petroleum. The plan would provide munitions, training and assistance to two elite Colombian army battalions to protect the pipeline, which Occidental claims has been attacked 700 times by rebel groups since it was built in 1986.</P>
<P>Colombian crude oil currently represents more than 2 percent of total U.S. imports. Arauca Mayor Jorge Cede&ntilde;o pointed out the hypocrisy of spending millions to protect oil profits while ordinary Colombians have suffered under 39 years of civil war: "How is it that they are bringing in troops for oil, and for the rest of the country--nothing? It seems that the U.S. government is interested in taking oil, and that's it."</P>
<P>Bill Clinton's administration made Colombia the third-largest recipient of U.S. aid, spending more than $1 billion on Plan Colombia, which was justified as part of the "war on drugs." But in the wake of the September 11 attacks, U.S. Ambassador to Colombia Anne Patterson made Washington's real motives clearer. "Colombia has the potential to export more oil to the U.S., and now more than ever, it's important for us to diversify our sources of oil," Patterson said last year.</P>


