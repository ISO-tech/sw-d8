<P><p><font face="Times New Roman, Times, serif" size="4"><b>An actor and a movie that still inspire us</b></font></P>
<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</P>
</B></I><P>Actor Gregory Peck died in June at the age of 87. A versatile and talented actor, to say that Peck left a void in the world of film is an understatement.</P>
<P>But it is Atticus Finch in the 1962 classic <I>To Kill a Mockingbird</I>, based on Harper Lee's novel, that most people associate with Peck. Peck's hero was not a flashy, muscular action star. He was bespectacled attorney hardly fitting the mold of "hero" by today's film standards. Nonetheless, his iron will and social conscience are absolutely inspiring to this day.</P>
<P>In <I>Mockingbird</I>, Finch is a lawyer in the rural town of Maycomb, Ala., during the Great Depression of the 1930s. Jim Crow is in full swing, and poverty is rampant. It is against this backdrop that Finch agrees to defend Tom Robinson, a Black man accused of raping a white woman. The odds are undoubtedly stacked against him. </P>
<P>Finch knows that Tom is innocent, and he knows he is the victim of a racist justice system. More importantly, he also knows that this is all the more reason to stand up for him. As the film progresses, Finch's children are brought into the harsh reality of their surroundings. It is the children, for example, who stop a late-night lynch mob from taking Tom, and possibly Finch, who stands watch outside the jail cell.</P>
<P>Released in 1962, the film stirred up controversy in the midst of the civil rights movement. Peck was an early outspoken supporter of civil rights in Hollywood. It is hard to watch Tom Robinson's trial without being reminded that we still live in a country that puts a bigger proportion of people in jail than any other country--most of them non-whites.</P>
<P>The sheer tenacity and unwillingness to back down is what is so memorable and beautiful about Gregory Peck's portrayal of Finch. Atticus Finch works from a simple, yet rock solid assumption--something that gives him strength in the face of great opposition and that no activist should ever forget: "I am right."</P>
<B><P>Alex Billet, </B>Washington, D.C.</P>
