<p><a name="Potato"></a><font face="Times New Roman, Times, serif" size="4"><b>Martha's only small potatoes</b></font></P>
<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</P>
</B></I><P>Recently, there has been a "major crackdown" on corporate criminals in the form of investigating the domestic diva, Martha Stewart, for charges of insider trading. Stewart, everyone's favorite homemaker, was the CEO of the company Martha Stewart Living Omnimedia Inc., until she was recently forced to step down because of the scandal (even though she still has a place on the board of the company--surprise).</P>
<P>Bush and his buddies want to show just how tough they are on corporate crime. But really, Stewart's crime is small potatoes compared to what ex-Tyco CEO Dennis Koslowski and Enron's Ken Lay are accused of. </P>
<P>Yet Martha has been constantly hassled by the media for months now, and there's not a single word said about the companies that were caught red-handed cooking the books. The reason for all the media hype around Stewart? Because she's supposed to represent all that is good and pure in the world. Well, that and the fact that she doesn't have ties to oil like so many of the other corporate crooks.</P>
<P>On top of all this hype around Stewart, there is not single word mentioned about the real crimes she's committed--using sweatshop labor and other seedy schemes to make as much profit as possible. </P>
<P>Ultimately, going after her in particular is not an attack on corporate crime. It's an attack on her own morality. And it surely is not an attack on the system of capitalism, as we know it. There's more to the Martha Stewart scandal than meets the eye. It's a (seemingly) cunning trick played by the corporate media and the Bush administration to try to make people think they're getting tough on corrupt CEOs.</P>
<P>Think again. </P>
<B><P>Julie Southerland, </B>From the Internet</P>

