<p><a name="Labor"></a><font face="Times New Roman, Times, serif" size="4"><b>Labor's struggle at home and abroad</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</I> </P>
</B><P>On June 6, a group of 50 trade unionists and activists gathered in New York City for a forum on labor rights in the U.S., Colombia and Venezuela. The forum was sponsored by the World Organization of People for Health Care and the Venezuela Solidarity Committee in New York, and included labor leaders from all three countries.</P>
<P>On the position of unions today, Raglan George Jr., executive director of AFSCME DC 1707, said, "It seems that the unions have taken a back seat," and added later that "we have got to go to the bargaining table together and present our demands, and make it known that if they are not met, we will shut [the city] down."</P>
<P>This includes the interests of workers in all countries. "Unions have to fight for all working-class people, and only with international solidarity can unions ever move forward," said Fernando Velez of Colombia. </P>
<P>Addressing some of the main problems facing unions and the working class today, George said, "This war on Iraq that we have engaged in is costing tremendous amounts of money, which is being paid for out of the pockets of workers." This forum really showed the possibilities of workers' power--and that it is something that can only be gained through sustained and unified fight among all of the working class.</P>
<B><P>Paul Grohman and Joe Ibanez, </B>New York City</P>
