<P><font face="Arial, Helvetica, sans-serif" size="2"><b>CHINA</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Mass protests shake Hong Kong's rulers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By David Whitehouse</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 11, 2003 | Page 5</font></P>

<P><font face="Times New Roman, Times, serif" size="3">HALF A million demonstrators forced Hong Kong chief executive Tung Chee-hwa back off on submitting a new law to impose major restrictions on civil liberties. Tung first withdrew key parts of the proposal and then--when his legislative support collapsed--postponed the bill indefinitely.</P>
<P>The protest, the biggest in China since the Tiananmen Square movement of 1989, braved sweltering heat July 1 to speak out against Tung's efforts to bring the island's legal system into line with the repressive tradition of the mainland. Many demonstrators called for Tung's resignation--and for direct election of representatives. Tung himself was placed in office by former Chinese President Jiang Zemin, and less than half of the local legislature is chosen by direct ballot.</P>
<P>Following the protest, Tung consulted with his masters in Beijing and decided to drop some of the most offensive parts of the proposed law. Tung won't seek the right to conduct "national security" searches without a warrant, and media outlets will be allowed to use a "public interest" defense when they are charged with treason for publishing government secrets.</P>
<P>As Hong Kong residents know, some of these secrets can be deadly. Many are still furious that officials concealed news of the SARS epidemic until after it had spread to Hong Kong from the mainland early this year.</P>
<P>Another source of anti-Tung feeling is the economic stagnation that has wracked Hong Kong since it reverted to Chinese rule in 1997 after a century as a British colony. Unemployment stands at 8 percent, and Tung, a free-market fanatic, has dismissed proposals for basic safety-net provisions such as unemployment insurance.</P>
<P>Tung's biggest concession may be in giving up the attempt to ban every group that is declared illegal on the mainland. This restriction would have outlawed everything from religious groups like Falun Gong and the Roman Catholic Church to labor unions and left-wing political parties.</P>
<P>Tung and the central government were planning to push the amended law through the legislature by July 9. Hong Kong is required to pass some sort of national security law under the mini-constitution written by Beijing following the 1997 "handover" from British rule.</P>
<P>But there's no deadline for the law, and organizers of the July 1 protest promised to surround the legislature July 9 to demand further discussion of the law before its passage. As many observers were predicting the new protest to be a more confrontational than the first one, the chair of the pro-business Liberal Party suddenly resigned from Tung's cabinet. Since the bill would have failed without Liberal support, Tung put off a vote indefinitely.</P>

