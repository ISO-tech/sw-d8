<P><font face="Times New Roman, Times, serif" size="4">Nine million out of work...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>And Bush doesn't care</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 11, 2003 | Page 1</font></p>

<P><font face="Times New Roman, Times, serif" size="3">IT'S THE longest period of job losses since the Great Depression of the 1930s. And it's getting worse. On July 3, the government announced that unemployment increased once again in June, reaching 6.4 percent--the highest level in nine years. All told, some 9.4 million people are out of work.</P>
<P>According to official figures, the U.S. economy is growing--but far too slowly to make up for the loss of some 3.1 million private-sector jobs since March 2001, with a net loss of 2.6 million jobs overall. "It's by far the largest contraction of jobs in the postwar period since the 1930s," Lawrence Mishel of the Economic Policy Institute (EPI) told <I>Socialist Worker. </I>"So the notion that this is a mild recession because unemployment isn't as high as other recessions is somewhat misleading."</P>
<P>As <I>New York Times </I>economics writer David Leonhardt put it, "for George W. Bush, the race has begun to escape comparisons to Herbert Hoover"--the Depression-era Republican president who was the last to preside over a decline in overall employment during his term in office. But don't worry. Bush says that he has a plan to create new jobs.</P>
<P>How? By lowering taxes on the very richest Americans, of course. By calling his $350 billion tax giveaway to the rich the "Jobs and Growth Act of 2003," Bush thinks the millions of the unemployed will be grateful for any crumb that falls off the tables of the super-wealthy.</P>
<P>That's an insult to people like Stuart Adkins. The 38-year-old lost his job as a lawyer in early 2001. He was forced to sell his home and most of his personal possessions to get by before landing a part-time job--at the local state unemployment office in Newark, Ohio.</P>
<P>Before getting that job, Adkins had been jobless for 14 months and wiped out his savings and retirement account. He sent out 350 resumes--and got just three responses. "You can only go so far on nothing," Adkins told the <I>Washington Post </I>last month. "I'm on the edge of struggling."</P>
<P>Even those who still have jobs are affected by the persistent unemployment, as employers used the threat of layoffs to drive down wage growth to less than the rate of inflation over the past year. Bush's tax cuts may boost the economy somewhat.      But there's no doubt that a huge chunk of the money will go into the pockets of the wealthy, rather than create new jobs.</P>
<P>"We need to get economic growth greater than 3.5 percent to get unemployment to decline," Mishel said--and most mainstream economists predict lower rates of growth. Moreover, unemployment benefits--despite their recent extension by Congress--could still run out for millions of the long-term jobless in the months ahead. It's time to demand longer and larger unemployment benefits--and demand government programs to create new jobs.</P>


