<P><font face="Times New Roman, Times, serif" size="5"><b>Behind bars in the land of the free</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 11, 2003 | Page 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3">LAND OF the free? Not exactly.</P>
<P>According to Justice Department statistics released in April, 2 million people are behind bars in the U.S.--a higher percentage of citizens than any other country in the world. For every 100,000 Americans, 702 are in jail or prison.</P>
<P>Maryland alone has more people in prison than all of Canada combined--despite the fact that Canada has a population that's six times larger than the state. And for all the Bush administration's talk about the terrible prisons of Iraq under Saddam Hussein, statistics show that the U.S. incarcerates a higher percentage of its population than any of the so-called "axis of evil" regimes. </P>
<P>And of course, the U.S. prison system is glaringly racist. One in eight Black men in their 20s and early 30s was in prison last year, compared to one in 63 white men from the same age group. </P>
<P>Since 1980, the prison population has quadrupled. This dramatic increase is due to two major factors: the push towards "three strikes" laws and lengthy sentences, and the "war on drugs."</P>
<P>In 1980, approximately 40,000 people were incarcerated because of drug offenses.  Today, that number is more than 450,000--with about 75 percent of those being Black or Hispanic. That's despite the fact that whites use drugs in equal numbers as compared to minorities. </P>
<P>According to the Justice Policy Institute, while prison spending increased by 166 percent nationally between 1985 and 2000, spending on higher education grew by just 24 percent. These newest statistics point out what activists have known for years: There's nothing just about the justice system. "This is a pretty serious experiment we've been engaged in," Vincent Schiraldi, director of the Justice Policy Institute, told the <I>Baltimore Sun. </I>"I don't think history will judge us kindly."</P>


