<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Neither a liberal nor a maverick</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 11, 2003 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE BIG political story in Washington today is the seemingly meteoric rise of former Vermont Gov. Howard Dean into the "top tier" of Democratic aspirants to kick George W. Bush out of the White House next year. In the three months ending June 30, Dean raked in $7.5 million in campaign contributions--more than any other Democratic candidate.</P>
<P>To the mainstream media at least, this makes Dean a real contender. Never mind that he's been running for president longer than any other Democratic challenger--and that he has excited Democratic audiences at a number of party gatherings over the last year.</P>
<P>This has Dean's opponents a little nervous--and led them to take potshots at him. "Everyone wants a race against Dean," said an adviser to right-wing Sen. Joe Lieberman (D-Conn.) "He looks easiest to bring down. He's positioned himelf as a liberal, and liberals don't win here." Lieberman's flak was echoing the words of the Democratic Leadership Council, the conservative grouping within the Democratic Party that has been carrying on against Dean for months.</P>
<P>To them, Dean is too "liberal" to win the presidency. If he gets the nomination, they argue, the Democrats can prepare for another 49-state blowout like they suffered in 1972, with George McGovern as their nominee. "The only thing more dangerous for the Democrats than Bush Lite is McGovern Extra Strength," <I>The Economist</I> warned.</P>
<P>But there is one major difference between Dean and McGovern. McGovern was a liberal. Dean isn't. "I don't mind being characterized as 'liberal,'" Dean told <I>Salon</I>. "I just don't happen to think it's true."</P>
<P>McGovern supported a guaranteed national income and national health insurance for all Americans. He opposed the death penalty. Dean takes none of these positions.</P>
<P>Dean has skillfully packaged himself as a "maverick," blasting his party for running away from a fight against Bush. In this way, he has tapped into a sense of frustration among large numbers of Democratic voters at the mealy mouthed incompetence of their so-called leaders in Washington. Yet striking a confrontational pose against Bush isn't the same thing as actually supporting programs to benefit working people.</P>
<P>Dean has been the most vociferous when pledging to repeal Bush's tax cuts. But rather than talk about using the money saved to fund essential programs, he pledges to balance the federal budget.</P>
<P>His plan to "save" Social Security is to raise the age at which seniors receive full benefits to 70. His health care plan spends less money and covers fewer people than either Rep. Dick Gephardt's (D-Mo.) or Rep. Dennis Kucinich's (D-Ohio) plans. He supports free trade; criticizes the Kyoto Treaty on global warming; and opposes only "parts," not all, of the USA PATRIOT Act.</P>
<P>With the weakening of unions and the shift rightward of the Democratic mainstream under Bill Clinton, "liberalism" has become more and more associated with social policies, like support for abortion rights and gun control--not with championing social welfare programs such as Medicare or Social Security. Most mainstream Democratic politicians peddle a "liberal on social issues, conservative on fiscal issues" position these days.</P>
<P>This position also appeals to parts of the Democratic "base" of upper middle-class voters and Wall Street money that have grown in importance over the last decade. Support for balanced budgets and opposition to the Christian Right motivates these constituencies, who aren't tied to the Democrats through the traditional means of unions or urban machines. Dean seems to be the favorite among these forces, which is the most likely explanation for his ability to raise $7.5 million from a base of 59,000 donors.</P>
<P>But one shouldn't mistake the image of an insurgent for the reality. "It's a pathetic thing that I'm considered the most progressive candidate" with a shot at winning the Democratic nomination, Dean told <I>Progressive</I> editor Ruth Conniff. Dean's right. Guess we'll know he's a real serious contender when he starts talking like the New Democrat he really is.</P>


