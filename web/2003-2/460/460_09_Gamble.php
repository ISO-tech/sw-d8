<P><font face="Times New Roman, Times, serif" size="4">Exposing the injustice of the death penalty</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Gambling with life and death</b></font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">Mike Gray, <B><I>The Death Game: Capital Punishment and the Luck of the Draw. </B></I>Common Courage Press, 2003, 216 pages, $19.95.</font></P>

<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Marlene Martin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 11, 2003 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">MIKE GRAY'S new book <I>The Death Game </I>focuses most of its pages on two important death penalty cases--Gary Graham (known as Shaka Sankofa) and Rolando Cruz. Sankofa was executed by the state of Texas in 2000 and Cruz, an Illinois death row prisoner, was eventually cleared of the crime that sent him to death row and released in 1995. </P>
<P>Gray's well-researched description of these cases presents an honest, non-legalese view of the workings of the death penalty. By taking us through a blow-by-blow account of how Shaka's case was fought through the courts, the absurdity of the legal system comes shining through. </P>
<P>Sankofa was sent to his death on the testimony of one woman who picked out his picture from five other photographs, saying, "That looks the most like him." She added that the man that she saw commit the crime had a "darker complexion," and his face was "thinner." </P>
<P>Later, she testified that she was absolutely sure Sankofa was the killer. But Dick Burr, Sankofa's lawyer for his appeals, found seven eyewitnesses who testified that Graham wasn't the killer. Despite an aggressive campaign through the courts and national exposure of his case, Graham was executed--without any of these witnesses being heard by a jury.</P>
<P>Gray also takes us through the courts' dealings with Rolando Cruz. The account of this case shows how far prosecutors will go to save themselves the embarrassment of admitting that they accused the wrong person.</P>
<P>Most galling of all is that they get away with it--and get promoted to boot! Gray points out that a <I>Chicago Tribune </I>investigation "found that over the last four decades, some 380 homicide convictions nationwide--67 of them death penalties--were thrown out because the prosecutors were caught covering up evidence of innocence or presenting evidence they knew was false.</P>
<P>This kind of deception is so reprehensible that the U.S. Supreme Court has said that it calls for criminal charges and disbarment. But nobody seems to be taking the high court seriously." As the <I>Tribune </I>concluded, "Not one of those prosecutors was ever convicted of a crime or barred from practicing law. Instead, many saw their careers advance, becoming judges or district attorneys. One became a congressman." </P>
<P>Another chapter in Gray's book talks about the Death Row Ten, Illinois prisoners who were tortured by police into confessions that were used to convict them and get them sentenced to death. Jon Burge, a Chicago police commander, was fired in 1993 when allegations surfaced that he and his police cohorts tortured Black men in interrogation rooms in Chicago.</P>
<P>Activism has been key to exposing Burge, but unfortunately, Gray fails to give any account of this, focusing instead on the legal and journalistic fronts. In fact, the role of activism is marginalized throughout his book.</P>
<P>In addition, there are some comments the book could have done without--for example, Gray's backhanded comments about former death row inmates as "punks" and "stick up artists." Gray likely writes this way because he never interviewed any of the former or current death row inmates that he writes about in his book.</P>
<P>This reviewer will also have to part ways with Gray on his views of a "natural" life sentence. He points out that in Michigan, where there is no death penalty, 2,500 people are serving life sentences without the possibility of parole. "In Michigan, life without parole means you exit feet first," he writes. If our criminal justice system gets it wrong in so many death cases, it only makes sense to conclude that there are injustices in other cases as well.</P>
<P>Despite these drawbacks, <I>The Death Game </I>is still a good read. It's one of those books that's difficult to put down. Anyone reading this book who does <I>not </I>believe that our criminal justice system is a mess will have a hard time keeping that point of view.</P>


