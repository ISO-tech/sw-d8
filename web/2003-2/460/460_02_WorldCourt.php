<P><font face="Times New Roman, Times, serif" size="5"><b>Strong-armed by Washington</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 11, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE BUSH administration thinks that the U.S. military should be above any law. And to prove it, earlier this month, Washington cut off military aid to 35 "friendly" countries that refused to agree to exempt U.S. soldiers from the jurisdiction of the new International Criminal Court (ICC).</P>
<P>The cuts were mandated by the American Service Members Protection Act (ASPA) that passed Congress last year after an uproar over the fact that some countries were standing their ground in saying that U.S. soldiers shouldn't be exempt from being tried before the new international court.</P>
<P>The ICC was set up in 1998 in order to prosecute war crimes, crimes against humanity and genocide. While 90 countries have ratified the treaty that formed the court, only 51 have so far bowed to U.S. pressure and signed agreements granting U.S. forces immunity.</P>
<P>In retaliation against the holdouts, any non-NATO country (with a few special allies excepted, including Australia, Japan and South Korea) has now had its military aid cut off. In the Bahamas, money for road paving and lighting an airport runway has been eliminated. And money that was destined for Caribbean programs for hurricane relief, rural dentistry and veterinary programs has been withdrawn.</P>
<P>"U.S. ambassadors have been acting like schoolyard bullies," Richard Dicker, director of the International Justice Program at Human Rights Watch, said in a statement. "The U.S. campaign has not succeeded in undermining global support for the court. But it has succeeded in making the U.S. government look foolish and mean-spirited."</P>

