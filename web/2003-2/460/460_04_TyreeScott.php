<P><font face="Times New Roman, Times, serif" size="4">Obituary</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Tyree Scott</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 11, 2003 | Page 4</font></p>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</P>
</B></I><P>Tyree Scott, an African American worker and community activist, has died at the age of 63 after a protracted and courageous battle with prostate cancer. Tyree was a fighter on a number of fronts, and his passing is an incalculable loss to workers in this part of the country.</P>
<P>During the 1960s and '70s, Tyree and several of his colleagues were on the front lines of the struggle to end discrimination in the building trades in King County. He was also in the forefront of hot labor battles at Harborview Hospital and the University of Washington. His legacy will also be seen in the Northwest Labor and Employment Law Office (LELO), which he helped to found with Silme Domingo and Gene Viernes in the early 1970s. LELO came together as part of an effort to put an end to the degradation of Filipino cannery workers. </P>
<P>Tyree and co-worker Todd Hawkins were later intensely involved in irrigation and other reconstruction projects in Mozambique in the 1980s, under the leadership of Samora Machel. In addition, Tyree is remembered for his poetry, written in a dry, forthright and caustic style.</P>
<P>Despite his long illness, he was engaged to the last with such causes as the recent longshore workers' lockout and the antiwar movement. Tyree Scott stood in the tradition of those rare U.S. labor leaders who knew how to walk that tightrope between the bureaucracy and the more dynamic forces among the ranks below. </P>
<P>A quiet, gracious, and personable man, totally lacking any capacity for self-inflation, he will be missed enormously.</P>

<B><P>Michael Hureaux, </B>Seattle</P>


