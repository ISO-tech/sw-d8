<p><font face="Times New Roman, Times, serif" size="4"><b>Defend Puerto Rican activists</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul Grohman</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Activists gathered on June 30 for an emergency picket to protest the arrest of Vieques activist Nilda Medina by the FBI. About 20 people attended the protest organized by the Vieques Support Campaign.</P>
<P>Medina--a founding member of the Committee for the Rescue and Development of Vieques--is recognized as one of the principal leaders of the struggle to get the U.S. Navy off of Vieques for more than two decades. The FBI arrested Medina on June 25, accusing her of violating Title 18 of the Federal Code for "damage of property."</P>
<P>Protest played a key roll in forcing the Navy to promise in May to finally leave the island. The struggle continues, though, as activists demand that the people of Vieques be given full control over the land the Navy used to occupy and that the Navy clean up the chemical, radioactive and other deadly substances that remain after decades of Navy weapons testing.</P>
<P>We must also organize to stop the persecution of political activists on and off the island. At the picket, chants of "&#161;Vieques s&iacute;! &#161;Marina no!" and "CIA and FBI, out of Vieques Puerto Rico!" echoed through the streets around the Jacob K. Javits Federal Building, which houses the Bureau of Citizenship and Immigration Services (formerly known as the INS).</P>

<I><P>For more information, visit www.viequessupport.org and www.prorescatevieques.org.</I></P>

