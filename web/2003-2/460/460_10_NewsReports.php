<p><font face="Times New Roman, Times, serif" size="4"><b>Stop Bush's war at home and abroad</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Wengraf</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>PHILADELPHIA--Some 5,000 people gathered in front of Philadelphia's City Hall to protest war at home and abroad on July 4. Protesters from United for Peace and Justice, Kensington Welfare Rights Union, Pennsylvania abolitionists, Millions for Mumia and others came together at the commemoration of the city's controversial new National Convention Center. The center was built on the site of a slave burial ground over fierce public opposition.</P>
<P>Protesters also demanded an end to attacks on immigrants, privatization and cuts in education and welfare. Supporters of death row inmate and political prisoner Mumia Abu-Jamal protested the anniversary of his conviction July 3, 1982, and his 21 years behind bars without justice.</P>
<P>George W. Bush was scheduled to appear but later dropped his plans to attend. "A military base is probably the only place Bush thinks he can go to avoid massive protest," said Phoebe Jones Schellenberg of the Global Women's Strike, a spokesperson for the protest. "But what he has to know is that veterans and military families are participating in the protests here, joining thousands of others in opposition to his plans for endless war, occupation, and other policies at home and abroad."</P>
<P>Plenty of other Republican bigwigs were on hand for the ribbon-cutting, including Supreme Court Justice Sandra Day O'Connor, Pennsylvania Sen. Arlen Specter and Gov. Ed Rendell. A falling display rushed Specter and Philadelphia Mayor John Street to the hospital with injuries, and the national media were only too happy to devote airtime to the accident, ignoring the thousands outside. </P>
<P>Injuries sustained by protesters also went unnoticed. Before the nonviolent march could even begin, police forcefully arrested Kensington Welfare Rights Union leaders Cheri Honkala and Galen Tyler. Authorities have charged them with felony offenses.</P>
<P>"The real dedication to freedom on July 4 lies with those who continue to resist Bush's war policies," said Robert Smith, director of the Brandywine Peace Community. "Enough of cruise missiles at the expense of funding for human needs. Enough of legislation like the USA PATRIOT Act that put a match to our civil liberties."</P>
