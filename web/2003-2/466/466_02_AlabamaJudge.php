<P><font face="Times New Roman, Times, serif" size="4">Chief justice of Alabama is no picked-upon "little guy"</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A fanatic with an agenda of bigotry</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 5, 2003 | Page 2</font> </P>

<P><font face="Times New Roman, Times, serif" size="3">AMID HOWLS of outrage from right-wingers last week, a two-ton monument bearing the Ten Commandments was removed from the rotunda of the Alabama state judicial building. Fanatical Alabama Supreme Court Chief Justice Roy Moore had snuck the monument--known as "Roy's Rock"--into the courthouse in 2001 and refused to remove it ever since. </P>
<P>He was finally suspended as a justice in August for defying a federal court order to remove the monument. News accounts have portrayed Moore as the "little guy," standing up to the powers that be. But Moore's "stand" is nothing more than an attempt to push a right-wing agenda and promote his own career.</P>
<P>In 1994, for example, when the ACLU sued him for having a Ten Commandments plaque nailed above his bench, Moore used the publicity--and his supposed stand for "state's rights"--to fan the flames for his appointment to the state Supreme Court. "To me, he didn't even know the law," Jim Hedgspeth, the district attorney in Gadsden, Ala., told the <I>New York Times</I>. "Most of the time, he would get the idea that the law books around him were there for decoration, not for use."</P>
<P>Moore used his position on the court to prove that he's a bigot of the first order. In a ruling last year that denied a lesbian custody of her child, Moore wrote that gay partners are "abhorrent," "immoral," "detestable," "an inherent evil" and "inherently destructive to the natural order of society." He cited biblical references and "direct revelation found in the Holy Scriptures" in making his ruling. </P>
<P>Moore isn't the first bigot to use the state courthouse in Birmingham as a stage. In 1963, Gov. George Wallace stood on the steps, proclaiming: "Segregation now, segregation tomorrow, segregation forever!" Moore is far from being the picked-upon victim of the federal government that he and his supporters claim.</P>
<P>Washington politicians have been complicit in Moore's religious temper tantrum. The U.S. House of Representatives, for example, recently passed a bill to bar federal funds from being used to enforce the court order to remove the Ten Commandments monument. And George W. Bush's fanatical attorney general, John Ashcroft, was totally silent about Moore's contempt for the Constitution.</P>



