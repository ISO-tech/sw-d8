<P><font face="Times New Roman, Times, serif" size="5"><b>Let them drink pesticides in India</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Ganesh Lal</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 5, 2003 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">DRINKING COKE or Pepsi can be hazardous to your health. Especially if you live in India. A recent study by the Center for Science and Environment, a New Delhi-based research group, showed that Coke and Pepsi products being sold in India "contained residues of four extremely toxic pesticides and insecticides: lindane, DDT, malathion and chlorpyrifos." The report stated that the drinks "had enough poison to cause--in the long term--cancer, damage to the nervous and reproductive systems, birth defects and severe disruption of the immune system."</P>
<P>Demonstrators gathered to smash soft drink bottles in the streets, yet the Indian government--eager to make nice with the corporate giants--gave the industry a "clean bill." Coke and Pepsi sell about 6.5 billion bottles a year in India. No wonder they are out to discredit the report. Pepsi even took out an ad in leading Indian newspapers claiming, "The safest thing you are likely to drink today is a Pepsi."</P>
<P>But most Indians are quite aware of the treachery of multinational corporations. In 1984, some 10,000 people died and nearly 120,000 were injured because of a gas leak at a Union Carbide plant in the Indian city of Bhopal. An investigation showed that the plant, which produced highly toxic pesticides, was poorly maintained and didn't stock sufficient quantities of the antidote that could have prevented many of these deaths.</P>
<P>In 1986, the Indian government filed a $3.3 billion suit on behalf of the Bhopal victims. But three years later, it settled for a measly $470 million--less than $3,000 per victim. Activists filed another suit, this time against Dow Chemical, which bought Union Carbide in 2001. But the civil damages lawsuit was dismissed last March by federal judge John Keenan. Human rights--and human lives--mean nothing to these profit-hungry corporations.</P>


