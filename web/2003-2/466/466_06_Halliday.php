<P><font face="Times New Roman, Times, serif" size="4">Former UN humanitarian aid coordinator speaks out:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"The UN failed the Iraqi people"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 5, 2003 | Page 6</font></p>

<P><font face="Times New Roman, Times, serif" size="3">DENIS HALLIDAY is the former United Nations (UN) coordinator of humanitarian aid for Iraq. After 30 years working at the UN, Halliday resigned in 1998 to protest the impact of UN-imposed sanctions, which each month killed 5,000 Iraqi children under the age of five, by the UN's own statistics. Halliday talked to <I>Socialist Worker's </I>ELIZABETH SCHULTE after the bombing of the UN's headquarters in Baghdad.</P>

<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">
<P>I WAS initially shocked and horrified at the attack on the compound, which is, after all, the place I worked myself. The corner of the building where the bomb went off is where my office was--probably the same office that Sergio de Mello [the UN envoy killed in the bombing] occupied, in fact. So it came home in a very real sense, given my own role in Iraq.</P>
<P>But the more I think about it, why am I surprised? Maybe some of us here try to envisage the UN in a benign light--as a positive force for good in the world. But of course, in the Middle East, this simply is not the perception that most people have of the United Nations.</P>
<P>In terms of Israel and Palestine, the UN is seen as an instrument of the U.S. and negligent of the resolutions that have been passed but neglected by Israel. The UN is seen as responsible for neglecting the Palestinians, failing to put in peacekeeping forces between Israel and the Palestinians.</P>
<P>In regard to Iraq, it's even more immediate--in that it was the UN that sustained sanctions on the Iraqi people for 13 years. Yes, we know that sanctions were driven by the Security Council, which is made up of member states, and those member states were, I think, coerced, corrupted and abused by the U.S. in particular into supporting sanctions for 13 years.</P>
<P>But those of us who worked in Iraq reported--along with UNICEF and others--the impact of those sanctions on the Iraqi people. And I would say that we, the United Nations, killed more Iraqis through UN sanctions--probably a million people, particularly children--in those 13 years than Mr. Bush the First, Mr. Clinton and then Mr. Bush the Second did with bombs.</P>
<P>We all have to remember this--that the UN is a legitimate target under those circumstances. It is an organization that has failed the Iraqi people--that has committed genocide in Iraq for many years, in keeping with the definition of genocide in the UN convention on genocide.</P>
<P>The other issue in Iraq is the collaboration between the UN and the U.S. in the country.  And given the fact that the aggression, invasion and occupation of Iraq is illegal, outside international law, incompatible with the UN Charter and not approved by the Security Council, any collaboration between the UN and the U.S. in Iraq is wrong...</P>
<P>There wasn't a family in Iraq that wasn't hurt by the sanctions. Many if not all families lost a child, cousin, father or mother prematurely and unnecessarily from bad water, lack of health care--all of the problems that the sanctions brought and sustained in Iraq for the last 13 years.</P>
<P>It's so easy for people like Blair and Bush, Rumsfeld and Cheney to blame Saddam Hussein for everything. But the fact is that it's actually the sanctions that destroyed the economy and the social services of this country.</P>
<P>The UN can only work in Iraq if <I>invited </I>to do so <I>by </I>the Iraqis--not the Americans, not the British, but only by the Iraqis. The terms of that participation has got to be determined by Iraq.</P>
<P>The Iraqis will want massive capital assistance to rebuild the damage of the first Gulf War, the second Gulf War and the 13 years of sanctions. They're going to need tremendous assistance, but they must determine who does that, how and when. That is their sovereign right as a proud and dignified people with an extraordinary history that we in the West--in this Judeo-Christian community particularly--have stomped all over in a manner that in my mind is grossly unacceptable.</P>


