<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>With "raving liberals" like this...</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 5, 2003 | Page 3 </font></p>

<P><font face="Times New Roman, Times, serif" size="3">HE'S THE product of elite prep schools and Yale University, born to privileges guaranteed by his wealthy and well-connected Republican father. As governor, he rammed through welfare "reform" and sacrificed urgently needed social programs to balance his state's budget year after year. And he's raking in so many contributions for his presidential run that he might bypass the public campaign financing system designed to give less well-funded candidates a chance.</P>
<P>Sounds a lot like George W. Bush. But it's Howard Dean, a leading candidate for the Democratic presidential nomination and the favorite of many liberals who want to see Bush beaten in 2004. Enthusiastic crowds of10,000 turned out to hear Dean in Seattle and New York City in late August, seeing in the former Vermont governor a clear alternative to Bush's policies of war and corporate greed. But they should take a closer look.</P>
<P>Dean's success is the result of his willingness to criticize both Bush--including opposing the U.S. invasion of Iraq--and the Democratic Party establishment's feeble opposition to the White House. But for each of Dean's proposals that challenge, for example, Bush's tax cut giveaway to the rich, there are other stands--like his call to raise the Social Security retirement age to 70 and his newfound support for the death penalty--that reflect the same Republican Lite policies he pushed as a moderate Democratic governor in the 1990s.</P>
<P>In truth, Dean is far from the "raving liberal" that he's made out to be by supporters and opponents alike. "One of my most persistent activities during the early 1990s was trying to fend off the more liberal wing of the Democratic Party," Glenn Gershaneck, Dean's former press secretary, told a <I>Washington Post </I>writer.<I> </I>"The idea that he is a raving liberal some folks around here [in Vermont] would consider a lunatic idea." </P>
<P>And anyone who thinks that Dean is a principled antiwar candidate needs to listen more closely to his speeches. In Seattle, he pointed out that he supported the first Gulf War on Iraq in 1991 and the U.S. war on Afghanistan--and he vowed that "as the commander in chief of the United States military, I will never hesitate to send our troops to any country in the world to defend the United States of America."</P>
<P>If Dean can be considered a "progressive"--or even "far left," as the conservative Democratic Leadership Council (DLC) puts it--it's because the entire Democratic Party has moved so far to the right over the past two decades. Under Bill Clinton, a Democratic Congress kiboshed health care reform--something that Dean is promising now--but pulled out all the stops for the NAFTA free trade deal and carried out the corporate agenda even before the Republicans took over Congress.</P>
<P>Since Bush took over the White House, the Democrats have caved again and again, on everything from tax cuts to the war drive against Iraq. This record shows the truth about the Democrats.</P>
<P>Despite their talk about standing up for working people, the Democrats are committed to serving the same corporate masters as the Republicans--even at the expense of those who support them. That's the reality behind the Democrats' campaign rhetoric--and it's why we need an alternative to the two-party system.</P>


