<P><font face="Times New Roman, Times, serif" size="4">Oppose California's racist referendum</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Vote no on Prop 54!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Katrina Yeaw</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 5, 2003 | Page 12 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">CALIFORNIA'S RIGHT-wingers are at it again. They have a new initiative--the deceptively titled "Racial Privacy Initiative," known as Proposition 54--on the ballot on October 7, the same day as the recall election against California Gov. Gray Davis. If passed, the measure would make it illegal for California government agencies, schools or employers to collect information on a person's race, ethnicity or country of origin.</P>
<P>The initiative is the brainchild of Ward Connerly, the University of California regent who was behind the passage of Proposition 209 in 1996, which put an end to affirmative action programs in public employment, contracting and education. Connerly and his pals claim that the issue of race is no longer relevant in California--and that passing this ballot measure would make government "colorblind."</P>
<P>But that's all a smokescreen for a racist measure that would make harm any effort to address the very real racial disparities that exist--in health care, education, hate crime and more. If Prop 54 passes, the state government would keep track of every form of discrimination <I>except </I>for discrimination on the basis of race and ethnicity--the number one source of discrimination reports. </P>
<P>Furthermore, banning the gathering of information about race and ethnicity would make it difficult to confirm that public agencies, schools and employers</FONT><FONT FACE="Arial" SIZE=2> </FONT><FONT FACE="Times">are complying with anti-discrimination laws. For example, efforts to track instances of racial profiling by police would be outlawed. "If it passes, it will place all the gains of civil rights in jeopardy," NAACP National Chair Julian Bond told the <I>San Francisco Chronicle.</P>
</I><P>The right-wingers' claim that racism doesn't exist is a fraud. We need to oppose their attempt to hide the issue of racism behind the guise of "privacy"--and build a broad movement to address the issues faced by people of color. Vote no on Proposition 54!</P>



