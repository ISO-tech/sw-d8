<p><a name="Families"></a><font face="Times New Roman, Times, serif" size="4"><b>Military Families Speak Out</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Cindy Beringer</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">CRAWFORD, Texas--Members of Military Families Speak Out had a simple message for George W. Bush, who was enjoying a month's vacation at his ranch here. "Bring the troops home now!"</P>
<P>The August 23 rally drew some 200 protesters, largely made up of family members who spoke about the role of oil and power as the true motives for the Iraq war and occupation. "Morale is low, our guys feel forgotten, and we need to show them someone does care," says Candance Robison, whose husband is an Army officer and engineer serving in Iraq.</P>
<P>Teri Allison's son enlisted in the Army as "an idealistic 21-year-old" and is due to go to Afghanistan. Allison worries about her son and his friends risking their lives in what she calls "an unjust and immoral war, based on lies and fabrications perpetrated by the Bush administration." Together, Robison and Allison called the protest.</P>
<P>Robison read a letter from a mother of a veteran of the first Gulf War who had participated in the murder of retreating Iraqi soldiers. "Mom, my tank killed more people than Tim McVeigh," her son had written her. "This was not a fair fight." The young soldier returned from Iraq and tried to kill his memories with drugs and alcohol. In 1996, he committed suicide, and his fianc&eacute; did the same a few months later.</P>
<P>The organizing efforts of Military Families Speak Out (www.mfso.org) and its affiliate group Bring Them Home Now (www.bringthemhomenow.org) bring a powerful message against the horrors of U.S. imperialism and show a deep concern for the suffering of the Iraqi people.</P>
<P>Robison urged military families to "be proactive...we must take to the streets again, and we have to stay there!"</P>

