<p><a name="Bush"></a><font face="Times New Roman, Times, serif" size="4"><b>Protest George Bush</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul Dean</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">PORTLAND, Ore.--More than 4,000 people turned out to protest George W. Bush, undeterred by the vicious police violence the last time Bush came to town. The 400 police who came out--many in riot gear with their pepper spray ready--cost the city tens of thousands in overtime pay, but didn't manage to arrest any more than 10 protesters.</P>
<P>Bush's three-and-a-half hour trip to Portland included a $2,000-a-plate fundraiser. This was a smack in the face for a state whose population has the nation's highest unemployment rate at 8.5 percent and the highest number of people without enough food to eat.</P>
<P>The event raised $1 million for Republicans, while Portland schools have implemented drastic cuts, and teachers worked 10 days without pay. One person in a nearby house put up a sign saying "Half price dinner: $1,000."</P>
<P>Ten protesters were arrested, and it was reported that some had jail support phone numbers removed from their arms. Bush can be assured that whenever he comes to Portland again, he will find a similar "welcome."</P>

