<p><a name="TeachIn"></a><font face="Times New Roman, Times, serif" size="4"><b>Anti-occupation teach-in</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Sid Patel</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">BERKELEY, Calif.--Some 55 people attended the first public meeting of the Berkeley Stop the War Coalition of the fall semester on August 26. Speakers included Max Elbaum, editor of the antiwar newspaper <I>War Times,</I> and student antiwar activist Snehal Shingavi.</P>
<P>Speakers touched on several important themes for the activists building a movement against the occupation in Iraq, including finding ways to expose the lies the Bush administration used to win its war on Iraq, informing others about the horrors of the occupation for Iraqis and U.S. troops, and linking the war and occupation to the White House's attack on workers at home.</P>
<P>Antiwar activists are making plans for a demonstration September 28 and a teach-in or debate to build for it. At the meeting, a proposal was made for the coalition to call for a Recall the War campus referendum, in which students would get to vote on "Money for War" or "Money for Schools."</P>

