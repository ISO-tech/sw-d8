<P><font face="Times New Roman, Times, serif" size="4"><b>Stop police brutality</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Susie Wasserstrom</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--The San Francisco Police Department is on the rampage again. On August 25, two police officers attacked 23-year-old Lee Collins in San Francisco's largely African American Hunters Point neighborhood. </P>
<P>The two cops punched, kicked and beat Collins with their billy clubs until he lost consciousness. Then, 30 more officers arrived on the scene, threatening residents who were watching this police terror. One cop pointed his gun at an 8-year-old. At least two more adults were brutalized, and a 14-year-old was sent to the hospital after being struck down by a billy club.</P>
<P>In response to this outrageous attack, community activists organized a press conference last week. About 60 supporters came out to tell their own stories of abuse at the hands of San Francisco and Oakland cops.</P>
<P>The SFPD showed up, too, and tried to intimidate the crowd by stationing themselves at both exits. But the crowd refused to back down and, instead, challenged the cops. "The officer that terrorized my child is back up on my street," said Susie McCallister, whose daughter was brutalized by Bayview police on Martin Luther King Day. "How dare you!"</P>
<P>Others directly addressed Deputy Chief Gregory Suhr, demanding to know why he was promoted after overseeing the 1998 police murder of Mike Garcia. A rally to demand justice for Lee Collins is planned for September 3 at 4 p.m. in front of the police station, 850 Bryant St.</P>
<I><P>For more information, contact Bay Area Police Watch at 415-951-4844.</I></P>

