<P><font face="Times New Roman, Times, serif" size="4">The sick reality of Washington's welfare "deform"</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Blaming the victims</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 5, 2003 | Page 4</font></p>

<B><P><font face="Times New Roman, Times, serif" size="3"> Dear <I>Socialist Worker,</I> </P>
</B><P>This week marks the seventh anniversary of the enactment of Temporary Assistance to Needy Families (TANF), the 1996 welfare "reform" legislation that ended the Aid to Families with Dependent Children entitlement program. Lawmakers have used the opportunity to brag about the "successes" of the program--namely the slashing of welfare caseloads by 50 percent within the first years of the program.</P>
<P>Because the law is up for reauthorization, conservatives are also chomping at the bit to make the program even more unreasonable, spouting ideological assaults on poor mothers that recall those leading up to the 1996 law. In addition to proposals to raise the work requirement from 30 to 40 hours a week for program participants, Bush and his ilk are eager to denounce any proposed innovation in that it might provide some flexibility within the strict requirements of the program.</P>
<P>For example, Bush recently blasted the Senate for introducing a proposal that would make going to college while on welfare a possibility for a mere 10 percent of recipients. The proposal would allow these recipients to count time spent in class for some of their required work hours. Paying for college would, of course, still be left up to the poor mothers.</P>
<P>Despite this pitifully modest idea, Bush was incredulous. "Some people could spend their entire five years...on welfare going to college." Bush scoffed. "Now that's not my idea of helping people become independent, and it's certainly not my view of understanding the importance of work."</P>
<P>You know it's a messed-up world when the wealth and power of the Bush family is enough to prove the "intelligence" and personal worth of George W. Bush--but he's allowed to assess the potential of poor people.</P>
<P>A large part of the rhetoric surrounding welfare reform was and continues to be an attack on the poor people themselves, turning public opinion against them for their alleged faults, rather than turning the scrutiny on the messed-up system that creates poverty. Rising unemployment, substandard wages, nearly nonexistent services for families and children in need will continue to take a back seat as the TANF reauthorization debate continues through September. It's time to make some noise about the real issues facing working people in society.</P>

<B><P>Katie Ray</B>, Madison, Wis.</P>


