<p><a name="Cone"></a><font face="Times New Roman, Times, serif" size="4"><b>Cone Mills' White Oak</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Brad Ward</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">GREENSBORO, N.C.--About 100 protesters assembled in the parking lot of Cone Mills' White Oak plant August 22 to protest cuts in double-time pay. Protesters were able to gather 500 signatures of workers refusing to give up pay.  </P>
<P>The White Oak plant produces denim and employs 900 workers who are constantly under the threat of having production moved to Mexico. The protest--organized by UNITE Local 1391--focused on the company's elimination of Sunday double-time pay and layoffs of hundreds of workers for two weeks.  </P>
<P>One worker explained to a local journalist that they rely on Sunday double-time wages to pay bills. With the future of another 900 jobs in North Carolina in jeopardy, it's imperative that Local 1391 and the local community stand united against the bosses that are making life for workers unbearable.</P>


