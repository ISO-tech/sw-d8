<P><p><font face="Times New Roman, Times, serif" size="4"><b>Tyson</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Bill Linville </b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">MADISON, Wis.--The University of Wisconsin-Madison's (UW) purchasing department and chancellor's office finally agreed in late August to implement a student government resolution to drop Tyson Foods products for the duration of Local 538's strike against the company.</P>
<P>UW purchases approximately $110,000 worth of Tyson products a year. A group of students has been in contact with UW purchasing about ending the purchase of Tyson products since late May.  </P>
<P>Despite a student government resolution and weeks of phone calls, activists were given the bureaucratic runaround. Finally, on August 8, activists flooded the UW purchasing office with phone calls.  </P>
<P>Administrators agreed to set up a "meeting" on August 15, expecting only four or five people to attend. Student activists and Tyson strikers organized about 50 people to come to the meeting and organized a picket in front of the administrators' offices the day before the meeting.  </P>
<P>Fearing a long, drawn-out battle with a group of dedicated activists and strikers in a community with a high level of support for the strike, school officials agreed to fully cut ties with Tyson. Activists should use this battle as a template for cutting ties with Tyson Foods and supporting strikers in their battle against Tyson's union-busting.</P>

