<p><a name="Ferry"></a><font face="Times New Roman, Times, serif" size="4"><b>Washington ferry workers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Steve Leigh</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEATTLE--More than 100 members and supporters of the Inland Boatmen's Union (IBU) rallied August 10 to protect 150 union jobs. The Washington State ferry system has subcontracted its kitchen work to a company unionized by the IBU, the marine division of the International Longshore and Warehouse Union (ILWU). But this October, the ferry system will for the first time end that agreement without a commitment to keep the work under a union contract. </P>
<P>The rally brought ILWU members together with several other unions, including Carpenters, Service Employees, Teamsters, Hotel and Restaurant Employees, federal and state workers and Jobs with Justice. "It was the rank and file that got the union active on this issue," said Jeff Engels, an IBU official. "We've had this jurisdiction for 50 years, and we're not about to give it up. Rank-and-file power!"</P>
<P>The stakes are high. As one worker said, "I make $13.80 an hour as a cook. If we lose this contract , we could lose our wages, and our pension could be in jeopardy."</P>
<P>The rally was planned to end in a march to the Port of Seattle office to protest the opening of a new non-union tour boat company, America West Steamship, on the otherwise unionized Seattle waterfront. At the last minute, the union forced that company to agree to a union contract, giving momentum to the struggle to save union jobs in the ferry kitchens.</P>

