<p><a name="Nurses"></a><font face="Times New Roman, Times, serif" size="4"><b>Vermont nurses</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul Fleckenstein</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">BURLINGTON, Vt.--By a 2-to-1 margin, the 160 licensed nurse practitioners (LPNs) at Vermont's largest hospital voted to unionize. The vote came one month after 1,200 registered nurses (RNs) at Burlington's Fletcher Allen Health Care won one of the best contracts in the country.  </P>
<P>Both groups are part of the AFT-affiliated Vermont Federation of Nurses. The priorities for LPNs are job security protections, reduced patient-nurse ratios and an end to mandatory overtime. In July, the RNs won their demands on all these issues.</P>
<P>Solidarity between the different nursing units was crucial. Five-year maternity nurse Anne Reay said that the RN contract contributed a lot to the LPN victory. "They were able to get the hospital to agree not to fight our union drive. We also saw what they were able to do, and we said we want that too."</P>

