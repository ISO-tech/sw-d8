<P><font face="Times New Roman, Times, serif" size="5"><b>"We want justice for Mario"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Roberto Barreto</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 5, 2003 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">PROTESTERS DEMONSTRATED at the state capital in Pheonix, Ariz., on August 26 to draw attention to the police murder of a 15-year-old Latino in Mesa. On August 25, Mario Madrigal's parents called police because their son had grabbed a knife and was threatening to take his own life. When police arrived, according to the parents, an officer shot Mario two times with a taser gun.</P>
<P>The teen fell to the ground. Then, without warning, cops started shooting with live ammunition, firing at least nine rounds. "He was already on the floor [when an officer] emptied his gun," said Martha Madrigal, his mother. "We called the police for help, and they shot him."</P>
<P>Last week's protest at the capital drew family and friends of the victim. "We want justice for Mario," said Martha Madrigal. "Justice means I want those killers behind bars". "They killed him in front of my eyes," said Mario Madrigal Sr. "If I didn't move my [son Bryant], they'd have killed him, too." </P>
<P>Since 2001, there have been at least three other police killings under similar circumstances in Arizona, where the young victim was mentally handicapped or mentally disturbed. And in Mesa earlier this year, police killed a young man and woman in a convenience store parking lot, claiming that the two represented a threat because they hadn't responded to a command to get out of their car. The fight for justice in the Madrigal case should be joined by all who want an end to police brutality.</P>



