<P><font face="Times New Roman, Times, serif" size="5"><b>Another Vietnam?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 5, 2003 | Page 1</font></p>

<P><font face="Times New Roman, Times, serif" size="3">"I THINK that Iraq, we have to be clear about this, is now shaping up as the worst foreign policy problem that the United States has faced since the end of the Vietnam War." That was the verdict of Richard Holbrooke, the former U.S. ambassador to the United Nations (UN) and one of the main architects of U.S. foreign policy for the last decade, on <I>Fox News Sunday</I>. What Holbrooke and other establishment figures are finally acknowledging is the huge gap between the Bush administration's rhetoric about Iraq and the reality on the ground.</P>
<P>Last week, Washington's overseer Paul Bremer claimed that Iraq "is not a country in chaos, and Baghdad is not a city in chaos." Tell that to the millions of ordinary Iraqis who continue to suffer without electricity, clean water and food--because their U.S. "liberators" have failed to provide the most basic necessities. Tell it to the thousands of Iraqis languishing in U.S. prison camps--left to sleep on the ground at night, and suffer through the stifling heat of an Iraqi summer during the day.</P>
<P>Even such White House allies as Sen. John McCain (R-Ariz.) had to admit last month that the suicide bombings of the UN headquarters in Baghdad and a Shiite mosque in the city of Najaf were symptoms of a crisis facing the U.S. occupation. Meanwhile, U.S. soldiers who were promised a "quick victory" are beginning to question why they remain in Iraq.</P>
<P>And Bremer himself revealed the enormous economic toll of the occupation when he told reporters that it will cost "several tens of billions" of dollars next year alone--money that will come from even deeper cuts in education, health care and other social services at home.</P>
<P>The reality is beginning to hit home for more and more people in the U.S. An August 23 <I>Newsweek </I>poll found that the percentage of Americans who believe that the U.S. should withdraw military personnel because of the UN attack had risen to 48 percent--a stark indication that growing numbers of people are seeing through the lies Washington uses to sell its occupation.</P>
<P>If all of this sounds familiar, it ought to. During the Vietnam War, the resistance struggle of the Vietnamese, combined with a growing international antiwar struggle and the revolt of U.S. soldiers against having to risk their lives for Washington's empire, led to a crushing defeat for U.S. imperialism.</P>
<P>Today, we have to build a similar movement. As a member of the Bring Them Home Now campaign, a coalition of military families and antiwar veterans' organizations, put it: "Our troops are being used as occupiers, in a nation that has shown itself to be stubbornly hostile to occupation...We've been here before. I was here before. Vietnam. Bring them home now. Leave Iraq to the Iraqis."</P>



