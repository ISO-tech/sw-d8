<P><font face="Times New Roman, Times, serif" size="4">U.S. steps up the pressure on North Korea</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bush's nuclear roulette</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By David Whitehouse</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 5, 2003 | Page 2 </font> </P>

<P><font face="Times New Roman, Times, serif" size="3">INTERNATIONAL TALKS over North Korea's nuclear weapons program ended in Beijing last week in a continued stalemate. The U.S. corporate media parroted the Bush administration line and heaped blame on the North Korean government for refusing to say that it would halt its nuclear weapons program.</P>
<P>Yet once again, it was the U.S. that took the toughest line in the six-party talks--involving delegates from the U.S., North and South Korea, Russia, China and Japan--by refusing to promise not to attack North Korea. This simple promise has been the North Korean government's main demand all along.</P>
<P>Just how hard would it be for Washington to agree not to attack North Korea? Impossible, apparently--since the "right" to pre-emptive attack is a matter of principle under the Bush Doctrine. Since coming to office, George W. Bush has declared his "loathing" of North Korean leader Kim Jong Il, listed the country as part of an "axis of evil" with Iraq and Iran, and disclosed plans to use nuclear weapons against the North in case of war.</P>
<P>The North claims the right to build nuclear weapons to defend itself against a superpower that has already overthrown two governments--in Afghanistan and Iraq. A North Korean foreign ministry spokesperson said last weekend that "both sides are leveling guns at each other" and that the U.S. wants the North to "drop its gun first." "Even a child," he said, "would not be taken in by such a trick."</P>
<P>At the talks, the U.S. negotiator, Assistant Secretary of State James Kelly, repeated charges that the impoverished regime was using nuclear "blackmail" to win economic aid from its neighbors. But the other countries involved actually want to develop the North's infrastructure--to shore up the weakest piece in the region's economic puzzle. It is Bush who has disrupted these development plans by beating the war drums.</P>
<P>U.S. officials also scoffed at the North Korean government's complaint that the U.S. is trying to "stifle" their country--at the very same time that Washington announced joint exercises with 11 countries next month to practice boarding commercial ships on the high seas, with North Korea as a main target of the initiative. The fact is that Bush wanted the Beijing talks to be useless--since the crisis gives him a chance to throw U.S. military and political weight around on the world stage.</P>


