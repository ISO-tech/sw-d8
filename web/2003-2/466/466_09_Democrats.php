<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Is a Democrat the antiwar choice?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 5, 2003 | Page 9 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">OF ALL the issues on which the 2004 presidential election will turn, none is likely to be more momentous than the issue of U.S. foreign policy. Thousands of people who marched and demonstrated against Bush's war are now signing up with various presidential campaigns, registering voters and the like in preparation for the election. Democratic presidential candidates like former Vermont Gov. Howard Dean and U.S. Rep. Dennis Kucinich (D-Ohio) are recruiting antiwar activists to their campaigns.</P>
<P>To many looking forward to voting Bush out of office in 2004, it's self-evident that the Democrats would present a reasonable alternative. After all, they claim, the real winner of the 2000 presidential election, former Vice President Al Gore, would never have pushed U.S. foreign policy in the direction that Bush has moved it, even after the September 11 attacks.</P>
<P>We have it on the authority of former Secretary of State Madeleine Albright, whose recent article in <I>Foreign Affairs</I> criticizes Bush for trading "reliance on alliance" for "redemption through pre-emption." But a closer look at both Albright's <I>Foreign Affairs</I> article shows that her criticisms of Bush are actually quite mild. </P>
<P>At several points in Albright's article, she asserts a criticism of Bush only to take it back a few paragraphs later. All of Albright's criticisms have the feel of someone who is still advising the president on how best to carry out administration policy.</P>
<P>It's worth keeping Albright's stance in mind when considering the Democratic critique of Bush's foreign policy that is emerging among the presidential candidates. The main themes are these:</P>
<P>--Bush's focus on Iraq has diverted attention and resources from the real battle: the "war on terrorism" against al-Qaeda;</P>
<P>--Bush has needlessly antagonized allies;</P>
<P>--Bush has failed to "finish what he started," from capturing Osama bin Laden to rebuilding Iraq;</P>
<P>--Bush is shortchanging homeland security.</P>
<P>What's missing? How about calling for the U.S. to get out of Iraq and letting the Iraqi people decide their own future? Or criticizing how Bush has used "homeland security" to cover for his attacks on civil liberties and workers' rights? Or calling for cuts in the bloated military budget to fund needed programs at home?</P>
<P>You can look in a lot of the fine print in all the statements from the "top-tier" candidates--Dean, Kerry and Gephardt--and you won't find them advancing any of these positions. None of these leading Democratic candidates or spokespeople challenges the assumption that the U.S. should be anything but the number one military and economic power in the world. </P>
<P>In fact, all of them call for an increased troop presence in Iraq--preferably staffed with NATO, United Nations or other foreign troops. They don't even rule out the idea of pre-emptive war. </P>
<P>Dean, the candidate who made his name by opposing Bush's in Iraq, said in a major foreign policy speech, "In November 2004, the American people will seek a president who is prepared to use our brave and remarkable armed forces, as I would, to defend against any actual or imminent threat to ourselves or our friends and allies."</P>
<P>Kucinich's campaign does take a more progressive stand on all of these positions than the others. But his campaign is not really aimed at winning the Democratic nomination. Instead, he wants to give progressives the idea that some Democrats actually care about what they think--only to make it easier for progressives to back a Dean, a Kerry or a Gephardt.</P>
<P>On this, Kucinich is very clear, recently telling the <I>Cleveland Plain Dealer,</I> "The Democratic Party created third parties by running to the middle. What I'm trying to do is to go back to the big tent so that everyone who felt alienated could come back through my candidacy." In other words, Kucinich's campaign is meant to recruit antiwar activists into a party that will nominate a pro-war, or certainly, pro-imperialist candidate to run against Bush. As antiwar activists hear the "anyone but Bush" mantra, they should remember this fact.</P>


