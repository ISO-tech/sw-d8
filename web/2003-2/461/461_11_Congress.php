<P><font face="Times New Roman, Times, serif" size="4">Solidarity rally shows support for Chicago's HERE Local 1</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Congress strikers dig in</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lauren Fleer and Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 18, 2003 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--More than 200 people gathered outside the Congress Hotel July 12 as the strike by Hotel Employees and Restaurant Employees Local 1 members entered its fourth week. Community and religious organizations sponsored this protest, which took its theme from the biblical story of the fall of Jericho. </P>
<P>Contingents from Jobs with Justice, Service Employees International Union Local 880, the Student-Labor Action Project and the ISO all turned out in good numbers. In a lively march, participants circled the building seven times toting signs and noisemakers, demanding that hotel management give a just contract to the 130 workers whose wages were slashed by 7 percent May 19.</P>
<P>The July 12 protest followed an earlier solidarity rally July 2 with 500 people from organized labor in attendance. Forcing low wages on workers is an old tactic by Congress Hotel owner Albert Nasser, whose Gelmart Industries runs garment sweatshops in Asia, Africa and Latin America.</P>
<P>But if Nasser's reach is international, so is solidarity for the Congress Hotel strikers. Gelmart workers in the Philippines held a two-hour rally July 2 in solidarity with the picket-line protest in Chicago.</P>
<P>Local 1 President Henry Tamarin--who was arrested on the picket line at the outset of the strike--has told workers that they face a long struggle. Neither side has initiated contact since the June 15 walkout.</P>
<P>But the workers' 24-hour picket line has impeded the hotel's ability to do business. Hotel management has stated that occupancy is averaging between only 20 and 30 percent, and this week the hotel was forced to shut down its restaurant for lunch and dinner, presumably due to an inability to staff it. This is an important victory for the picketers, who have played a direct role in ushering customers to other hotels, as it illustrates a weakness and vulnerability on the side of management.</P>
<P>Nevertheless, more is needed to win. One important way to bring pressure on management is to organize solidarity action by the unionized building maintenance staff, who are members of the International Union of Operating Engineers (IUOE) Local 399, and the security guards, who are members of the Service Employees International Union. Members of both unions are crossing the picket line, which weakens the strike.</P>
<P>So far, Local 1 hasn't asked these unions to honor their picket lines. But while management would try to target workers who take action, support from the Chicago Federation of Labor (CFL), could keep those workers from being isolated. In fact, CFL President Dennis Gannon is a member of another IUOE local in the city.</P>
<P>While Congress Hotel workers are determined to fight as long as it takes, management hopes that the low-wage workers won't be able to afford a long battle. That's why it's important to step up the solidarity action now.</P>
<P>A union victory at the Congress Hotel would provide a recent example to all Chicago workers that organizing and solidarity can beat the bosses' stranglehold. A win at the Congress would be a win for all of us, and workers from every industry have good reason to support this strike.</P>


