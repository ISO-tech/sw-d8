<P><font face="Times New Roman, Times, serif" size="5"><b>Censoring a "Marxist rant"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Ganesh Lal</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 18, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">RIGHT-WING students are attacking academic freedom in North Carolina. A small group calling itself the Committee for a Better Carolina is up in arms over the University of North Carolina-Chapel Hill's decision to assign Barbara Ehrenreich's book <I>Nickel and Dimed</I> as a summer reading project for incoming freshman.</P>
<I><P>Nickel and Dimed</I> is a brilliant expos&eacute; of the plight of the working poor in the U.S. But to these students, it is "a classic Marxist rant" and "an all-out assault on Christians, conservatives and capitalism." So they said in a full-page ad in a local newspaper, paid for by the ultra-conservative John William Pope Foundation.</P>
<P>This isn't the first time that conservatives targeted the reading list at UNC-Chapel Hill. Last fall, a Virginia-based Christian fundamentalist group sued the university for assigning a book about Islam called <I>Approaching the Qu'uran</I> as summer reading.</P>
<P>UNC grad student and activist John Cox told <I>Socialist Worker </I>that while there is no direct connection between the two attempts at censorship, last year's controversy seems to have inspired campus conservatives.</P>
<P>But UNC-Chapel Hill also has a tradition of progressive activism--and students are fighting back with a series of actions, Cox said. He called the attack on <I>Nickel and Dimed</I> "a sign of desperation and of frustration" from right-wingers who "lose considerable sleep when they look around and see anti-war teach-ins [drawing] 200 people."</P>
