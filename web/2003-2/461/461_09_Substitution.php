<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>No substitute for workers' power</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 18, 2003 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WHEN THE First International Workingman's Association was formed in the mid-1860s, Karl Marx inscribed on its banner: "The emancipation of the working class must conquered by the working classes themselves." The exploited and oppressed must free themselves--that was Marx's starting point.</P>
<P>They cannot be freed by "philanthropic bourgeois" reformers. They cannot be freed by a heroic guerrilla force. They cannot be freed by a small band of terrorists with dynamite. And they cannot be freed by a party that stands above or outside their own struggles.</P>
<P>The English version of the anthem of the international socialist movement puts the same idea in the negative: "We want no condescending saviors." Or as the American socialist Eugene Debs put it: "If I can lead you into the promised land, I can lead you out again."</P>
<P>Marx's phrase underscored an idea that was fundamentally different from a whole number of radical and socialist ideas current then--and which are, in various forms, still current now. These ideas can be roughly placed under the category of "substitutionist." In Marxist terms, any group, class or party that substitutes its own activity for that of the working class--that acts as a proxy in the name of and on behalf of the working class--is substitutionist. </P>
<P>In the early days of capitalism, when the working class was in formation and had yet to systematically assert itself in collective struggle, substitutionist ideas were inevitable. The utopian socialists, for example, substituted their own schemes for constructing a better world in place of workers' own struggles, seeing workers as merely a "suffering class" that would be not the creators of the new world, but merely its beneficiaries.</P>
<P>In the more active revolutionary movement, Marx and Engels contended with the conspiratorial radicalism of the French socialist Auguste Blanqui and the Russian anarchist Mikhail Bakunin.</P>
<P>Engels, for example, criticized the Blanquist notion that saw revolution coming from "a small well-organized minority, attempting a revolutionary <I>coup de main</I> at the right moment," and that would "carry the mass of the people with them." Marx and Engels offered withering criticism of Bakunin's idea that a secret society of "one hundred serious and firmly united revolutionaries" could act as the spark for a Europe-wide revolt.</P>
<P>The argument against substitutionism was not an argument against leadership or organization. Collective action is not possible without both. The question was how to build organization and leadership that was part and parcel of the working class and its struggles, not something built outside of it.</P>
<P>The problem with Blanquism was that it tended to see revolution not as the culmination of a process of mounting struggle that required certain favorable conditions, but rather as something that could be willed into being. "Instead of the actual conditions," wrote Marx, "<I>pure will</I> becomes the drive-wheel of the revolution for them."</P>
<P>"Whereas we tell the workers, 'You have 15, 20, 50 years of civil wars and people's struggles to go through, not only to change the conditions, but in order to change yourselves and make yourselves fit for political rule,' you say on the contrary: 'We must come to power right away.'"</P>
<P>Marx's criticism of substitutionism did not simply involve a moral revulsion against elitism. He understood that only through collective, self-conscious mass action by workers themselves would it be possible to overthrow the existing society and found a new one.</P>
<P>Only through mass action would workers be able to "change" their "conditions." Capital gives nothing without a fight. Moreover, collective struggle was necessary for workers "to change yourselves and make yourselves fit to rule."</P>
<P>Collective action changes consciousness. It instills in workers a sense of their own potentialities and awakens in them a desire to break the stultifying chains that capitalism imposes on their bodies and minds. It breaks down the divisions of language, race and sex that keep workers apart--and begins to create people capable of reshaping a new world.</P>


