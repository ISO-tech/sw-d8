<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Scandal over war lies exposes Washington's imperialist agenda</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The empire unmasked</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 18, 2003 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">THE SCANDAL over George W. Bush's fraudulent claim about Iraqi efforts to develop nuclear weapons is laying bare Washington's biggest lie of all--that its war was about anything other than oil and empire. As <I>Socialist Worker </I>went to press, criticism of Bush was mounting--despite the White House's best efforts to put out the fire.</P>
<P>Washington's warlords have been exposed as willing to say and do anything in their drive to dominate the world--so much so that even the meek American media finally had to take notice at the outrage around the world. Weapons of mass destruction and alleged nuclear programs were always a cover for Bush's real aims--strengthening U.S. power by taking control of the world's second-largest oil reserves.</P>
<P>But control of oil isn't an end in itself. The Bush administration's hawks recognize it as a means of achieving the goals laid out in last year's National Security Strategy document--preventing the emergence of any strategic rival in Europe or Asia. In other words, the war is an outgrowth of the world imperialist system--a system dominated by the U.S., but which also includes great powers in Western Europe and Japan.</P>
<P>The rivalry between imperialist powers was the real reason for the friction between the U.S. and Europe over the Iraq war--not the supposed refusal of the French to get their hands dirty, as American right wingers would have it. In fact, French soldiers are today imposing the imperialist world order in eastern Congo and the Ivory Coast in Africa--under the guise of "peacekeeping." This supposed humanitarian intervention  masks what's really at stake--France's effort to preserve its role as the dominant force in resource-rich parts of its former colonial empire.</P>
<P>Similarly, a possible U.S. intervention in Liberia won't be motivated by concern for the suffering caused by a civil war. Washington set the stage for slaughter in Liberia in the 1980s by backing the dictatorship of Master Sgt. Samuel Doe, whose repressive government provided the CIA with an African base for its efforts to destabilize Libya and roll back national liberation movements in Southern Africa.</P>
<P>Since then, the U.S. has sought "stability" in the region by supporting Nigerian-led "peacekeepers"--even blessing the warlord Charles Taylor's seizure of power in Liberia. If the U.S. has now turned against Taylor, it's because the  war threatens to destabilize an oil-rich region that is increasingly important to the U.S. economy.</P>
<P>That's why it's wrong to call for U.S. intervention in Liberia--even on humanitarian grounds. The Pentagon that would move into Liberia is the same one that smashed its way through Iraq and rules with an iron first today. Giving support for a "humanitarian" military intervention today only gives the U.S. more political cover to conduct its next pre-emptive attack.</P>
<P>The Bush administration may decide that Africa is not important enough for it to intervene. But you can bet that Iran, with its strategic location and immense oil reserves, will  remain a target.</P>
<P>Behind this "war on terror"--which will last decades, according to the White House--is the dynamic of the world capitalist system itself. Capitalism is based on competition--and in a global economy dominated by a small number of giant multinationals, economic rivalry drives political, and ultimately military, competition between nation-states as well.</P>
<P>The capitalists of the advanced countries share an interest in squeezing weaker and smaller states through loans and "structural adjustment programs" pushed by the International Monetary Fund and World Bank. But the big powers compete among themselves as well, both economically and politically, if not yet militarily.</P>
<P>The arguments between the U.S. and Europe over steel tariffs, for example, and the Iraq war, are two sides of the same coin--a struggle between a band of hostile brothers, as Marx called the capitalist class. All this has made the world a more unstable place--with more wars to come as a result of the U.S. drive for global domination. Antiwar activists need to seize on the Iraq war scandal and take the argument further--opposition to the entire project of U.S. imperialism.</P>


