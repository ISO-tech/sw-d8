<P><p><font face="Times New Roman, Times, serif" size="4"><b>Brutality of the system</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">IT WAS horrifying to hear of the deaths of 19 undocumented workers from Mexico and Central America, suffocated in the trailer of an American truck ("Nightmare in Texas," May 23). It reveals the naked brutality of the world we live in, in which millions of refugees are regularly forced to risk their lives to make a living. </P>
<P>The Texas border incident is eerily similar to one described by the Palestinian writer and activist Ghassan Kanafani in his 1963 short story, "Men in the Sun." Kanafani tells about a group of desperate Palestinian men who try to smuggle themselves across the desert in an oil tanker in order to find jobs in wealthy Kuwait. </P>
<P>The author uses the confined space of the truck as a symbol for the way in which the Israeli occupation entraps ordinary Palestinians and suffocates them. Nations occupied and exploited by imperialists and their allies are turned into death traps, resources are stripped away, jobs become scarce, and ordinary people die, whether they remain inside or try to escape. We have to expose border restrictions for what they are--tools for the ruling classes of different nations to control the flow of labor and to multiply their massive profits. </P>

<B><P>Pranav Jani</B>, Glen Ridge, N. J.</P>

