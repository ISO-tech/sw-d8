<p><a name="Rape"></a><font face="Times New Roman, Times, serif" size="4"><b>Our duty to make Northeastern act</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">CURRENTLY AT Northeastern University, there are zero reported rapes. Since statistics show that one out of every four women can get raped during their time at campus, this means that on a campus of thousands, either Northeastern has excellent security, or rapes are going unreported. </P>
<P>There is almost no place to go to at Northeastern for someone to report a rape. The only thing that Northeastern has to offer are two police officers trained in sexual assault. Thus, if alcohol or drugs are involved, then no one wants to go to the police and possibly get blamed. </P>
<P>What Northeastern University lacks is a rape crisis center or even a rape crisis counselor. Despite Northeastern University President Richard Freeland's claims of "students come first at Northeastern," he seems not to care about women being raped on campus.</P>
<P>The Student Government Association (SGA) and the Resident Student Association (RSA) have both passed resolutions to hire a rape crisis counselor. These resolutions have been signed by Freeland, yet since next year's budget for Northeastern has no proposed spending on a counselor, Freeland seems unwilling to take any real action.</P>
<P>Thanks to Freeland's inability to take action, women have been going to the Feminist Student Organization (FSO) for support. Despite the FSO's commitment towards giving support, none of them are trained in rape crisis counseling. Quite frankly, the FSO shouldn't be asked to counsel when that is something that should already be taken for granted at a university.</P>
<P>Since Freeland doesn't want to take action, the students have decided that it is our duty to make Northeastern act. Over the last few weeks, the FSO, SGA, ISO and a few other groups on campus have been organizing to tell Freeland that the students at Northeastern demand a rape crisis counselor.</P>
<P>On May 20, a speakout was organized to tell Freeland exactly how students feel about rape on campus. Over 300 students walking by the speakout stopped to sign a petition to tell Freeland that there needs to be a place to go to report rape and get counseling. Since Freeland has utterly failed in his promise to the women at Northeastern to take action, it is up to the students to demand that Northeastern makes a rape crisis counselor available.</P>

<B><P>Ben Larrivee</B>, Boston, Mass.</P>

