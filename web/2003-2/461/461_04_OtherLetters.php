<p><a name="Benefits"></a><font face="Times New Roman, Times, serif" size="4"><b>Why are retirees losing benefits?</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">I THINK it's unfortunate that steelworkers and other retirees are losing benefits and support from the companies they built and toiled for so many years. Medical benefits and prescriptions are necessary almost daily for the aging generations, and what do they have to turn to now that their benefits are being dissolved? Nothing! </P>
<P>It's unfortunate that our more mature retirees now make up a good portion of the millions of Americans with no health care coverage at all! This should outrage the public more, but is anyone noticing? More and more companies and corporations continue to cut benefits, and their hardworking employees get the short end of the deal. </P>
<P>Is it an issue of rising health care costs or companies worried about corporate overhead vs. profit? I found out that 65,000 employees of the state of Arizona Department of Corrections may lose health coverage in June 2003, due to the $300 million it is costing the state. Where will it end?</P>
<P>Are the employees of America's great corporations doomed to take care of health care coverage themselves? Who will fill in the gaps? If possible, I'd like to be able to get more information on the corporate restructuring of Bethlehem Steel and the route that retirees are going to take to cover their health and prescription needs.</P>

<B><P>Phil Landry</B>, from the Internet</P>

