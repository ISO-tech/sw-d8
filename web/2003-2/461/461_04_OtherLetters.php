<p><a name="Dems"></a><font face="Times New Roman, Times, serif" size="4"><b>The Democrats aren't a solution</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">I APPLAUD your decision to publish the letter from Donald Williams (June 20) as, I assume, an opportunity for us to respond to statements from the liberal point of view. </P>
<P>First, we do not "fix the problem" of corporate greed by electing corporate Democrats. We fix it by mass action. Second, while it is true that "a socialist today has no chance of winning nationwide office," voting for Democrats will never change that. Only mass action will. Dr. Williams is wrong to focus on national elections as a tool for change. </P>
<P>Third, yes, we do "remember Florida," but what we remember is not the corporate media lie that the Green Party stole the election from the Democrats, but rather the illegal actions taken by Jeb Bush's Florida government to deny voting rights to thousands of people and the refusal of the Democrats to confront the issue. </P>
<P>Yes, it will take a long time to build a movement sufficiently strong to effectively oppose corporate rule. But electing Democrats is not even a step in that direction.</P>

<B><P>Susan Bassein</B>, Davis, Calif.</P>

