<p><a name="Money"></a><font face="Times New Roman, Times, serif" size="4"><b>Will money exist under socialism?</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">I READ Eric Ruder's article "How would we make a real democracy?" (<I>SW</I>, May 23) with interest. Nonetheless, the statement that "delegates" of the workers' councils would receive the same "compensation" as "their coworkers" raised an eyebrow. If by compensation you mean money, then is Ruder suggesting that money will have some place in a socialist society? </P>
<P>If so, there is no need for money, as Marx made a good argument for its termination in <I>Capital</I> and <I>Grundrisse</I>. I've always envisioned a system of credits, which could then be used to obtain property, goods and services. </P>
<P>Any differences in "compensation" would come in higher credits to persons who work longer hours (with inheritance outlawed so as to prevent unnecessary hoarding of wealth). That way, one need not worry about ensuring "delegates" make the same as their "co-workers," as it would already be built into the system.</P>

<B><P>David Bliven</B>, Oceanside, N.Y.</P>

