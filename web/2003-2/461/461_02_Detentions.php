<P><font face="Times New Roman, Times, serif" size="5"><b>Victims of an anti-Arab witch-hunt</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Maryanne Zarrella</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 18, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE BUSH administration's disgusting scapegoating of Arab and Muslims has been on full display in the Seattle area. Last month, Mohammad Kamrah Tavakoli-Nouri was deported back to Iran--after spending three months in detention.</P>
<P>The Bureau for Citizenship and Immigration Services, (formerly the Immigration and Naturalization Service (INS), apparently thought the computer programmer with a heart condition was a threat to national security. Mohammad emigrated to the U.S. 24 years ago. He applied for citizenship in 1998 and had also applied for political asylum. While in detention, he spent half of his time in solitary confinement against the recommendations of his doctor. Authorities at the jail also withheld prescribed nitroglycerin tablets. </P>
<P>One month before Mohammad was deported, three Homeland Security agents abducted Abdel Saleem Atmani in front of the restaurant where he works. He was handcuffed, and dragged off to the Seattle detention center.</P>
<P>Abdel came to the U.S. from Algeria to escape mandatory military service. In the four years that he has been here, he applied for political asylum and checked in with the INS every three months.</P>
<P>The conditions at the detention center are so inhumane that Abdel decided to sign his deportation order--even though he is likely to face jail time if he is returned to Algeria. Detainees are held in rooms with 25 other people, he says, and lights are left on in the room from 6 a.m. to Midnight. Detainees are allowed to outside only twice a day. "People need to stand up--otherwise, they'll end up in here," Abdel told Socialist Worker. "The U.S. is not a paradise. Everyone is not free. It's all bullshit."</P>
<P>Meanwhile, members of the Hamoui family are continuing to fight the U.S. government's effort to deport them back to Syria. Public pressure played the decisive role in stopping the nine-month-long detention of Safouh and Hanan Ismail Hamoui and their 19-year-old daughter Nadin. Community groups organized weekly pickets, and a coalition of many different groups came together to bring the story of the Hamoui family out into the open. </P>
<P>Yet the Hamouis still face the threat of deportation--even though they would likely be tortured or killed if they returned to Syria. In fact, the family has been applying for political asylum since it came here in 1992.  But that didn't stop John Ashcroft and his thugs from trying to throw them out of the U.S. for overstaying their visas.</P>
<P>The family is determined to continue their fight. In June, Nadin testified at a congressional hearing in Washington, D.C., along with other victims of the post-September 11 witch-hunt of Arabs and Muslims. "We are all human, we want to raise our families and make a good life, so why this discrimination?" Safouh Hamoui said in an interview with <I>Socialist Worker. </I>"Man makes borders, not God. We need to come together as humans. Everybody needs to unite to stop these attacks."</P>
