<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 18, 2003 | Page 11</font></p>

<p><font face="Times New Roman, Times, serif" size="4"><b>Hamilton Sundstrand</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">ROCKFORD, Ill.--A six-week lockout at an aeronautics plant here ended in defeat for United Auto Workers Local 592 after members voted overwhelmingly to accept concessions that they had earlier rejected.</P>
<P>The nearly 800 members of Local 592 were locked out at Hamilton Sundstrand May 19 after a 90 percent vote to reject a contract that called for an end to retiree health benefits and an increase in health insurance premiums for workers. But on June 25, workers voted by a 6-to-1 margin to accept a deal that contained most of those concessions.</P>
<P>Under the new five-year deal, Hamilton Sundstrand--a subsidiary of United Technologies--will eliminate retiree health care after 2008. In addition, workers will have to pay an additional 15 percent more in health insurance costs <I>every year</I> between 2004 and 2008.</P>
<P>These additional expenses will eat up the pay gains of the contract, which include a $1,500 signing bonus and a 15 percent raise over the course of the five-year deal. If this wasn't bad enough, management hadn't returned all the machinery moved out of the plant during the lockout, which means the Rockford plant's work could have been moved to other locations.</P>
<P>What's more, Local 592 recommended a yes vote even though the contract isn't entirely settled--negotiations are expected to continue until September 1. The lesson of this battle is that small union locals can't be left alone to fight giants like United Technologies, whose bosses were calling the shots. Local 592 did get support from the Rockford labor movement--but labor is going to have to use real muscle to defeat vicious employers like these.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Tyson Foods</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Peter Couvares</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">JEFFERSON, Wis.--Support is growing here for the 470 striking workers at the Tyson beef processing plant here . Donations continue to pour in from unions around the country--and after almost six months on strike, virtually none of strikers have crossed the picket line.</P>
<P>People outside the labor movement are taking notice. The Associated Students of Madison, the student government at the University of Wisconsin, has passed a resolution in support of the strikers, and demanded that the University stop buying Tyson products. Research by students and workers on campus shows that the University spends over $100,000 a year on products from the company.</P>
<P>On July 7, the Madison School Board voted 5-0 to ban the purchase of Tyson products in the school district until the company settles the strike in favor of the workers. And in mid-July, following a rally of 100 people outside the City-County Building shortly before the County Board meeting, the Board of Supervisors voted 23-9 to support the strikers, and to halt the county's purchases of $36,000 a year of food from Tyson.</P>
<P>The university is next. Since the administration has so far refused to end its purchases of Tyson products, students and labor activists have formed a solidarity committee to push for a boycott, and are beginning to organize regular trips to the picket line.</P>
<P>The South Central Federation of Labor, the regional labor council which was one of the first in the country to support the Charleston Five dockworkers' struggle, is also forming a solidarity committee to actively support the strike. These are the kind of efforts we need to step up the fight and build solidarity in the Tyson workers' fight for justice.</P>

