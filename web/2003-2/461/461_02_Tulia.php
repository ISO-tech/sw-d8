<P><font face="Times New Roman, Times, serif" size="4">Tulia drug sting prisoners finally freed</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Racism of an unjust system</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mike Corwin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 18, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IN AN act of long overdue justice, the state of Texas has freed all but a few of the men and women still behind bars who were arrested in the notorious drug "sting" in the town of Tulia. The 12 people released in June were among 46 Tulia residents who were arrested in 1999--when police rounded up about 10 percent of the town's African American population on drug charges.</P>
<P>No drugs were found during any of the arrests, and the only evidence against the defendants was the word of a single white narcotics cop named Tom Coleman. Nevertheless, 38 women and men were convicted for drug trafficking.</P>
<P>As the Tulia arrests slowly gained national attention, the cases began to unravel, and attention focused on Coleman, who faces felony perjury charges. Yet the convictions would never have happened in the first place if not for prosecutors and the presiding judge--who together suppressed evidence that would have exposed Coleman as a chronic liar.</P>
<P>The 12 who were released last month were freed not by the Texas court system correcting its mistake, but because of a bill passed by the Texas legislature and signed by Gov. Rick Perry. The 38 who received jail sentences or probation still have criminal convictions hanging over their heads, while the appeals courts and Perry's Board of Pardons and Paroles drag their feet.</P>
<P>And there is no help for the victims of this injustice to put their lives back together. Joe Moore, a 60-year old hog farmer, was fingered as the kingpin of the Tulia "drug ring." </P>
<P>Now finally released from jail, he said that he doesn't know what happened to the 350 hogs that he had on his 30-acre farm. "I've been locked up," Moore told a reporter. "I want to go home, look at TV and stay out of trouble."</P>
<P>The Tulia case has exposed the racism and corruption of the "war on drugs"--and the Texas injustice system. "Tulia is a stark reminder that the racism that spawned and perpetuated the enslavement of people based on their skin color continues to skew justice," the <I>Austin American-Statesman </I>wrote in an editorial.</P>
<P>"[The 12 who were released] are living symbols that innocent people are sent to prison, maybe even to the death chamber. They are reminders that Texas justice is not colorblind, but rather blinded by color."</P>


