<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Does a Democrat deserve your vote?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 18, 2003 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">WHAT ARE you willing to swallow to get rid of George W. Bush? That's what the question comes down to for many people looking ahead to the 2004 election--and trying to decide which of the Democratic Party candidates they can stand to vote for.</P>
<P>Naturally, people on the left want to get rid of Bush. From the bloody colonial conquest of Iraq, to tax breaks for the rich, to anti-union attacks, millions of people have good reason to want to stop him any way they can.</P>
<P>So it's no wonder many have concluded that the left should vote for a Democrat--any Democrat, no matter how conservative--as the "lesser evil" in 2004. But just how "lesser" is the "evil"? In the case of Sen. Joe Lieberman (D-Conn.)--Al Gore's running mate in 2000 and the leading conservative Democrat running for the nomination--there's very little difference between him and the Bush White House, on anything from the Iraq war to proposals to reform health care.</P>
<P>The pathetic record of Lieberman and the other Washington insiders in the Democratic field explains the emergence of Howard Dean, the former Vermont governor who has built a following because of his willingness, unlike his rivals, to sharply criticize Bush. This "straight talk" may be appealing. But a look at Dean's record shows that he's not the liberal he's made out to be. For example, he opposes universal health care as too expensive--and has even proposed raising the retirement age to 70 to "save" Social Security.</P>
<P>Running to Dean's left is a group dismissed by the Washington establishment as "fringe candidates"--Rep. Dennis Kucinich (D-Ohio), Rev. Al Sharpton and former Illinois Sen. Carol Moseley-Braun. Their positions are more in step with the views of activists in the antiwar and global justice movements. </P>
<P>Kucinich, in particular, is winning support from activists. Earlier this month, for example, a group of well-known women writers led by Barbara Ehrenreich issued a statement titled "Feminists for Kucinich." Oddly, the statement implies Kucinich's support for a woman's right to choose abortion--without mentioning his long history as a supporter of the anti-abortionists, until he "changed his position" recently.</P>
<P>Nor does it come to terms with the fact that Kucinich is an extreme long shot to win the Democratic nomination--since the big-business interests that control the party's purse strings despise his liberal politics. Anyone who doesn't admit this is avoiding the key question--of what they would do when Kucinich fails to win.</P>
<P>The Feminists for Kucinich statement concedes that left wingers should "vote your head in November." Translation: Oppose a left-wing third party campaign, like Ralph Nader's in 2000, and support whatever Democrat emerges with the nomination, no matter how conservative, in the name of defeating Bush.</P>
<P>This dynamic shows how--rhetoric aside--Kucinich's real role in the Democratic Party is to round up the left and deliver it to the eventual nominee. This isn't a new phenomenon. Rev. Jesse Jackson's 1984 and 1988 presidential campaigns mobilized Black voters and the left in the primaries--then funneled them to the lackluster moderates Walter Mondale and Michael Dukakis.</P>
<P>During both campaigns, radicals believed that Jackson's Rainbow Coalition would be the launching pad for a left-wing political alternative. Both times, they were proved wrong when Jackson fell in line behind party leaders.</P>
<P>This contributed to the rightward shift of the whole spectrum of national politics--and postponed yet again the urgent task of building an alternative to the two corporate parties. That's why the politics of lesser evilism is a trap--one that the left should resist.</P>

