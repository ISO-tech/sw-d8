<P><font face="Times New Roman, Times, serif" size="5"><b>We should celebrate gay rights decision</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 18, 2003 | Page 4</font></p>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</P>
</B></I><P>I was surprised to read about the gay rights victory in the Supreme Court under the headline "Should we celebrate?" (<I>SW,</I> July 4) While the court's ruling on affirmative action was mixed, underlining theoretical support for affirmative action while barring any meaningful way to quantify it, its decision around gay rights was unequivocal and perhaps should have been elaborated on with a bit more attention in another article.</P>
<P>The court's overturning of a Texas anti-sodomy law is a major victory for gay rights and should be celebrated as such. In fact, thousands of lesbians and gays did, turning out for impromptu celebration rallies all over the country to recognize this victory.</P>
<P>Legally speaking, the court has brought the U.S. into the 20th century by finally decriminalizing gay sex. This ruling breaks open the opportunity to win other important legal rights like adoption and gay marriage, which have long been denied by lower courts.</P>
<P>The argument has run that, if gay sex is still illegal, then how could a judge go on to allow gay parenting or marriage? An important legal hurdle has been lifted. But this is, of course, the problem that <I>SW</I> was absolutely right to point out. </P>
<P>The fact that the gay rights movement has been limited to legal victories around sex and marriage shows how far it has been beaten back. In fact, the Supreme Court's recent ruling itself was on the most conservative basis possible: that of the right to privacy, not the right to liberation. For lesbian and gays to win genuine rights that are guaranteed in reality and not just on paper, we will have to rebuild a fighting movement from the ground up.</P>

<B><P>Jeff Bale, </B>Washington, D.C.</P>

