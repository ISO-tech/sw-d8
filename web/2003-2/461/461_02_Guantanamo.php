<P><font face="Times New Roman, Times, serif" size="4">U.S. announces plan for secret tribunals</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The Pentagon's kangaroo court</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By John Green</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 18, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">SIX CAPTIVES at the U.S. government's Camp X-Ray penal colony at Guant&aacute;namo Bay, Cuba, have been handpicked by George W. Bush to be tried before secret military tribunals. According to a Pentagon announcement at the beginning of July, the six will be the first "enemy combatants" to face the kangaroo-court system that Bush created by executive order following the U.S. war on Afghanistan.</P>
<P>To get ready, the military is pressing ahead with plans to construct a permanent prison--with a death row and execution chamber--at the U.S. base. The Pentagon's "rules" for the tribunals would be laughable if they weren't so sick--and such a blatant attempt to set an example for stripping all people of their rights, U.S. citizens or not.</P>
<P>Already, Bush has granted the Pentagon the right to hold "enemy combatants" indefinitely without any legal rights. Because of "relaxed rules" of evidence, military prosecutors will be able to use statements obtained through torture and the testimony of anonymous witnesses.</P>
<P>Evidence can be withheld from defendants for national security purposes--on the prosecutors' say-so alone. Defense lawyers for detainees will come from the same source that is jailing them, as well as providing the prosecutors and judges: the U.S. military. </P>
<P>Prisoners do have the option of retaining a civilian lawyer--but under the most absurdly limited circumstances. The civilian lawyers must be U.S. citizens, they won't be compensated for their work, they must be cleared for "secret" access, and they can't leave Guant&aacute;namo or even request a continuance once tribunals begin.</P>
<P>To top it off, even if detainees are somehow acquitted by a tribunal, Bush has the power to overturn the ruling--and order the person executed. And there is absolutely no right to appeal.</P>
<P>The Pentagon's announcement caused an uproar in Britain because two of the first six prisoners to be tried are British citizens. Officials from Prime Minster Tony Blair's Labour Party government--the lapdog ally of the Bush gang during both the U.S. war on Afghanistan and on Iran--have been forced to complain to Washington about the tribunals.</P>
<P>But U.S. officials aren't making any concessions. We can't let the Bush gang get away with using these secret kangaroo courts without a fight.</P>


