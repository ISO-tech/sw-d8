<P><font face="Times New Roman, Times, serif" size="4">Liars in the White House</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Get out of Iraq now!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 18, 2003 | Page 1</font></p>

<P><font face="Times New Roman, Times, serif" size="3">THOUSANDS OF Iraqis killed by U.S. bombs and bullets. A population lacking basic necessities like electricity and running water. A whole country under the iron heel of a military occupation.</P>
<P>And what was it all for? Lies.</P>
<P>The antiwar movement said it all along--that the Bush gang was using the flimsiest of manufactured evidence as a justification for war. Now that fact is starting to seep into the pro-war corporate media.</P>
<P>In particular, the White House is on the hot seat for including in George W. Bush's State of the Union address the false claim that Iraq tried to obtain uranium for nuclear weapons from the African country of Niger. Earlier this month, former ambassador Joseph Wilson wrote in the <I>New York Times </I>that his CIA-directed mission to Niger exposed this claim as a lie--and that U.S. and British officials ignored his findings in their campaign to justify an invasion of Iraq.</P>
<P>The Bush spin doctors are scrambling to contain the mess. They got CIA chief George Tenet to take the blame--even as CIA insiders told the press that the White House itself was pushing to use the fake claim. Meanwhile, outgoing White House Press Secretary Ari Fleischer served up more Orwellian doublespeak, insisting that "the burden is on those people who think [Saddam Hussein] didn't have weapons of mass destruction to tell the world where they are."</P>
<P>To National Security Advisor Condoleezza Rice, the claim about Iraq's purchase of uranium was "just one small justification" for going to war that didn't matter all that much. And in a sense, she's right. The faked Niger story <I>was </I>just one lie--one lie out of dozens that Washington told over and over and over again in order to whip up support for their war for oil and empire.</P>
<P>Like the lie that Saddam Hussein was connected to al-Qaeda and was somehow responsible for the September 11 attacks in the U.S. Or the lie that Iraq could deploy weapons of mass destruction in 45 minutes. Or the lie that Iraq's regime was a threat to neighboring countries, and even the U.S. Or the lie that the U.S. wasn't going to war because it wanted Iraq's oil.  Or the lie that Washington cared about "liberating" the Iraqi people. All lies--repeated almost without question by the mainstream media, before, during and after the invasion.</P>
<P>The truth can be seen now in the "new" Iraq--a country with a ruined economy, critical shortages of food and water, a new council of U.S.-backed stooges installed as figureheads, and the U.S. military in charge of an occupation growing more violent and bloody by the day. We have to expose all the lies about the U.S. government's war for oil and empire--and build an opposition that demands an end to Washington's colonial occupation.</P>


