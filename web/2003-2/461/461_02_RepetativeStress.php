<P><font face="Times New Roman, Times, serif" size="5"><b>Bush gives green light to unsafe conditions</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 18, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE BUSH administration took a swipe at workers' safety on the job July 1 when  it revoked a requirement that companies keep track of ergonomics injuries. According to regulations issued by the Occupational Safety and Health Administration (OSHA) in 2001, employers have to check a box in their workplace injury log if an employee  suffers from an ergonomics injury--which include repetitive stress injuries, such as carpal tunnel syndrome.</P>
<P>After years of foot-dragging in Washington, workplace ergonomics regulations--though watered down--were finally enacted in the last days of the Clinton administration, only to be suspended by the incoming Bush team. The Bush administration argued that companies could be relied on to come up with their own voluntary measures to ensure workplace safety.</P>
<P>The requirement that employers were supposed to record injuries was supposed to alert them to ergonomic problems. Now the White House says that employers don't even have to keep track.</P>
<P>The attempt to block ergonomics regulations has been funded by powerful corporations, like United Parcel Service, which would rather see workers injured than lose profits. Obviously the Bush administration feels the same way.</P>


