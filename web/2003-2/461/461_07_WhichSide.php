<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The class war from Reagan to Bush</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 18, 2003 | Page 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">GEORGE W. BUSH bears more than a passing resemblance to Ronald Reagan--and not just because of their shared difficulty pronouncing four-syllable words. Bush's presidency is modeled on Reagan's--as a ruthless and one-sided class war against workers and the poor. </P>
<P>As president from 1981 to 1989, Reagan oversaw the key years of the transformation of the U.S. working class from the best paid to among the worst paid in advanced industrial economies. Between 1972 and 1993, the real wages of young male workers--among the hardest hit--fell by 25 percent.</P>
<P>Today, Bush's tax cuts for the rich--like Reagan's--are just one part of a multi-pronged assault on the entire working class, aimed at trampling on workers' rights and decimating what little remains of the social safety net serving the poorest of the poor. But the stakes today are even higher--since Bush's assault is aimed at a working class whose living standards have already been severely eroded and whose unions have been pummeled.</P>
<P>Real wages began falling again last year and have been ever since. More than 2 million workers have lost their health care coverage since the recession began, bringing the total number of Americans without insurance to 43 million. Although the output of the U.S. workforce expanded at an annual rate of 4.2 percent since the end of 2001, the layoffs continue, with unemployment rising to 6.4 percent last month.</P>
<P>While the recession and fallout from September 11 provided the official excuses, the real reason that even the most profitable companies are slashing wages and benefits is the swelling number of unemployed. All in all, 2.6 million jobs have been lost since the recession started in March 2001--90 percent of them in manufacturing--in the longest period of sustained job loss than at any time since the Great Depression of the 1930s. African-American unemployment grew to 11.8 percent in June--reaching a staggering 39.3 percent for Black teenagers.</P>
<P>In February, the highly profitable Tyson Foods demanded wage cuts of $2 an hour, alongside cuts in health and pension benefits from workers at its Jefferson, Wis., plant, prompting the first strike in the factory's 128-year history. "The fact that they're making record profits doesn't seem to matter," striker Chuck Moehling--who, after 22 years on the job, earns just $27,000 a year--told the <I>New York Times.</P>
</I><P>Management agrees. "We're not saying the Jefferson plant is losing money," said company spokesman Ken Kimbro. "We're saying the cost in Jefferson is out of line, and we have to make adjustments."</P>
<P>The human casualties of these and other corporate "adjustments" include workers facing deadly diseases like lupus and cancer. As recently as three years ago, most companies paid health benefits for workers who became disabled. Now corporations are firing disabled workers and terminating their coverage.</P>
<P>Today, 27 percent of corporations fire workers as soon as they go on long-term disability; another 24 percent fire them within 12 months. Last July, Polaroid fired all its employees on long-term disability in preparation for a takeover by Bank One.</P>
<P>Veteran employee John Magenheimer received his pink slip as he lay in bed "in a fog" from chemotherapy for a cancerous tumor. "How could Polaroid do this to me?" he asked. "For more than 20 years I gave them everything I had."</P>
<P>But corporations' only loyalty is to the pursuit of profits. In an atmosphere of job insecurity and with a cheerleader in the White House, they believe that they can get away with it.</P>
<P>Only one thing can stop them--active resistance on the part of workers themselves. The Tyson workers have been fighting back since February 28 in a strike that has brought the working-class community together to draw the line against corporate greed.</P>
<P>Jefferson residents, local restaurants and supermarkets have donated food to help sustain the strikers and their families, while scores of supporters display signs in their front yards reading, "No greed," and "We need more than chicken feed." This small strike in a small Wisconsin town is pointing the way forward for us all.</P>



