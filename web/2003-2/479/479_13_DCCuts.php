<P><font face="Times New Roman, Times, serif" size="4">"Devin's life probably could have been saved"</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A cutback that killed</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 12, 2003 | Page 13</font></P>

<P><B><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Devin Fowlkes, an 11th grader and football player at Anacostia Senior High School who fell victim to a stray bullet after a pep rally, may have been able to survive if the doors of D.C. General Hospital's trauma center were still open. D.C. General was only four minutes away from where Devin was shot--but its trauma center was a casualty of Mayor Anthony Williams' massive overhaul of the city's health care system. </P>
<P>According to reports, it took 15 minutes for the ambulance to reach Howard University Hospital on Georgia Avenue. Little more than an hour later, Devin Fowlkes was dead. "Maybe it would have made a difference if D.C. General was open," said Rodney West, a paramedic for 15 years. "He was very viable. His life probably could have been saved."</P>
<P>The tragedy of Devin Fowlkes puts an all-too-human face on an issue that affects members of my community in Prince George's County. While the wealthier western part of Washington, D.C., has three adult trauma centers, the eastern half--where poverty and stray gunfire are all too common--has none.</P>
<P>The eastern half, of course, borders our county. There has been much talk by police in recent months about criminal justice "cross-border" initiatives to "get tough on crime." </P>
<P>But what about health justice? Why no trauma centers? This is an outrage.</P>
<P>Williams, who scaled back services at D.C. General over the unanimous objection of the D.C. City Council and dozens of protests, should listen very carefully to the words of Fowlkes' mother Marita Michael.</P>
<P>"There's got to be a hospital here," she said, "because I don't want any other parent to go through what I'm going through. Time is of the essence when something like this happens, and everybody knows that." Marita Michael said this as she sat in her Anacostia townhouse, wearing a shirt that pictured her son and the words "A New Angel in Heaven."</P>
<B><P>Dave Zirin, </B>Prince George's County, Md.</P>



