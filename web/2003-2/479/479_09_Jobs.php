<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why the economy isn't creating jobs</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 12, 2003 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IF THE economy is growing so fast, why is the job market still so lousy? Back-to-back economic reports earlier this month showed that the U.S. economy grew at an annual rate of 8.2 percent from July to September, the fastest pace in 20 years--but that it produced a net gain of just 57,000 jobs in November.</P>
<P>Apologists for the Bush administration argue that job growth is often last to come in an economic recovery. That's correct as far as it goes. But it leaves out the fact that the time lag from the end of the recession in 2001 to a slow recovery in jobs earlier this year was more than two years, the longest since the Second World War.</P>
<P>Moreover, the jobless rate of 5.9 percent isn't dramatically lower than the high of 6.4 percent seen earlier this year--and would be higher if the 1.5 million people "marginally attached to the labor force," as the Bureau of Labor Statistics calls them, were counted. According to the Economic Policy Institute, a total of 2.3 million people have either been forced to withdraw from, or delayed entry into, the workforce--which would put the real jobless rate at 7.4 percent.</P>
<P>So why hasn't red-hot growth produced more jobs? The answer is that the U.S. economy is in the midst of an aggressive restructuring that's squeezing more profits out of fewer workers, while using currency devaluations and trade barriers at the expense of American rivals.</P>
<P>To ease the pain, Washington has devised an economic Novocaine: stimulating the economy through tax cuts and a big spike in military spending, while keeping interest rates low to stimulate housing and consumer spending. Of course, there's another aspect of the Bush economic agenda--transferring wealth from workers to the rich and creating a fiscal crisis that will justify more cuts in government spending and "reforms" like the partial privatization of Medicare.</P>
<P>The stimulus was timed to heat up the economy just ahead of the 2004 election. Even so, there is a basic logic at work in the Bush economic plan. Faced with a worldwide glut of goods from autos to airplanes to telecommunications cables and more, business has been reluctant to invest in new production that would stimulate jobs.</P>
<P>Instead, the economy had to be propped up by the lowest interest rates in 40 years, allowing workers and the middle class to increase consumer spending by refinancing mortgages and taking advantage of special  "zero percent" auto financing. Businesses cut costs by shutting factories and slashing jobs while using low rates to work off debts accumulated during the 1990s boom.</P>
<P>The low interest rates and growing federal government deficits contributed to a decline in the value of the dollar--with the Bush administration's blessing--on international markets, with the dollar falling more than 30 percent relative to Europe's currency, the euro, since 2001 (but not, however, relative to China and most East Asian countries). The result is that U.S. exports are cheaper relative to Europe's, which has helped slow the loss of factory jobs in recent months.</P>
<P>U.S. business investment in equipment and software also jumped in the third quarter, after two-plus years of decline and stagnation. Yet new investment in "structures"--factories and the like--was barely in positive territory, and far from making up for the long slump in such spending since the recession began in early 2001.</P>
<P>Productivity, however, has soared, with an average increase of 5.4 percent over the past two years. In the late 1990s, productivity growth was attributed to the miracle of technology. With spending on such gear depressed, it's clear that employers boosted productivity the old-fashioned way--by making workers toil harder for less, and limiting new hires as much as possible.</P>
<P>There are other hurdles for the U.S. economy too numerous to discuss here--most importantly, trade wars and the reliance on international investors to finance the U.S. trade and budget deficits.</P>
<P>The recovery is real. But for workers, the economy is going to feel like a recession for some time to come--unless we organize to do something about it.</P>



