<p><a name="Empire"></a><font face="Times New Roman, Times, serif" size="4"><b>No to occupation and empire</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--More than 300 people gathered at Barnard College to hear author Tariq Ali and others speak out against U.S. empire. Ali spoke about the Iraqi resistance to occupation and argued for building "the broadest possible front against empire." He pointed out that while the situation in Iraq is not identical to the Vietnam War, "once you start to occupy a country, the pattern of resistance is the same."</P>
<P>Tariq was joined by Lou Plummer of Military Families Speak Out, Sarah Zaidi from the Center for Economic and Social Rights and Meredith Kolodner of the ISO. Lou described the impact of the war on his hometown of Fayetteville, N.C., near Fort Bragg and argued that "you can't rebuild a country from behind a machine gun."</P>
<P>At Columbia University, more than 40 students attended a debate on the question "Should U.S. troops leave Iraq?" Representatives of the College Democrats, the ISO, the College Republicans and the Columbia University Antiwar Coalition took part in the debate. The debate ended with a closing poll that showed the majority of the audience opposed to the occupation, while a handful of students wrote that their opinions shifted during the debate.</P>
<I><P>Laura Durkay, Lee Wengraf and Suzie Schwartz contributed to this report. </P>
