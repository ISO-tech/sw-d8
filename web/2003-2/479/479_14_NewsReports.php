<p><a name="Resist"></a><font face="Times New Roman, Times, serif" size="4"><b>Critical Resistance</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Wallis</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--City cops descended on the offices of a civil rights group November 16, starting a melee that ended in numerous injuries and eight people under arrest. The police showed up at the Critical Resistance space in Brooklyn as a fundraising party for an anarchist conference was taking place.</P>
<P>According to Mayuran Tiruchelvan, who was interviewed on the left-wing radio program <I>Democracy Now!, </I>three plainclothes detectives entered the space at 2 a.m.--supposedly responding to a report of someone standing outside with an open container of alcohol--and took one person outside to put him under arrest. When several people went outside to find out what was going on, within seconds two dozen police cars arrived.</P>
<P>The police then began indiscriminately clubbing and punching people, and, according to the Critical Resistance Web site, police sprayed about 20 people with chemical agents. Arrestees were charged with inciting a riot, obstruction of governmental administration, resisting arrest and disorderly conduct--and several had to be treated for a number of serious injuries, including a spinal injury and severe blows to the head. </P>
<P>Tiruchelvan speculated that the brutal assault took place because Critical Resistance is organizing protests during the Republican National Convention in New York City next September. The arrestees will appear December 18 at Brooklyn Criminal Court, 120 Schermerhorn St., Room AP-2, at 9 a.m. Critical Resistance is hoping for a good turnout of supporters.</P>



