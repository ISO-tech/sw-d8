<P><p><font face="Times New Roman, Times, serif" size="4"><b>Free Farouk!</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Wengraf</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Prison guards assaulted longtime Palestinian activist Farouk Abdel-Muhti November 19. The guards were carrying out a search of immigrant detainees' cells in the Bergen County Jail in Hackensack, N.J., when they found leftwing and revolutionary publications. The two officers began to attack Abdel-Muhti, calling the publications "anti-government" and telling him to "shut the fuck up" and "go back to Palestine." </P>
<P>The officers then pushed Abdel-Muhti against the wall, kicked him to the ground and punched him in the head. They confiscated his personal property, including papers, address books and prescription medicine. Prison authorities then filed a disciplinary report against him. Abdel-Muhti is 56 years old, in poor health and was denied his medication for at least five days after he was attacked.</P>
<P>Nineteen months ago, Abdel-Muhti was arrested under the same repressive rules established by the Department of Homeland Security that have led to the detention and deportation of thousands of immigrants--mainly Muslims and Arabs--since September 11. Immigration authorities claim that Abdel-Muhti violated a 1995 deportation order, but Abdel-Muhti was targeted because he is an outspoken critic of U.S. support for Israel's occupation of Palestine. </P>
<P>Most recently, Abdel-Muhti had a radio show on Pacifica network's local station during which he was broadcasting interviews with residents of the Occupied Territories. The Committee to Free Farouk recently held a rally in Newark, N.J., to keep the pressure on. Don't let the authorities keep this fighter behind bars!</P>

