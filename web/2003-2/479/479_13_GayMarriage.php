<P><font face="Times New Roman, Times, serif" size="5"><b>What socialists should say on gay marriage</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 12, 2003 | Page 13</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</I> <BR>
</B>Jeff Bale missed the mark in his article ("Behind the furor over gay marriage," <I>SW, </I>December 5) on the victory for gay marriage in Massachusetts.</P>
<P>We wish that Bale had been in Massachusetts in the days following the Supreme Judicial Court (SJC) ruling. The euphoria surrounding the ruling was contagious--as was the sense that we had just won an historic victory against a right wing that has been on the war path ever since the Vermont civil unions ruling five years ago. Polls done in the hours following the victory show that 54 percent of Massachusetts citizens support gay marriage.</P>
<P>There are three main points that we take issue with in Bale's article. First, it is important to note that this is the first time in the history of the U.S. that gays and lesbians have the right to marry! Massachusetts has gone much further than the Vermont law (which was a separate-but-equal ruling). </P>
<P>Only a handful of companies offer domestic partnership benefits, including health care, right now. But with marriage, <I>all </I>companies large and small will be made to give families of their gay employees health care coverage (where they offer it at all). This is a huge victory for our class and a blow to the right wing. </P>
<P>The anti-gay bigots, from President Bush to Gov. Mitt Romney on down, plan to pull out all the stops. They have already begun working on amendments to both the Massachusetts and Federal Constitutions officially banning gay marriage. </P>
<P>Massachusetts is seeing the most disgusting show of "bipartisanship" when it comes to this amendment. Thomas Finneran, a Democrat and speaker of the House, was the first to jump onto Romney's bandwagon. And the notoriously anti-gay Catholic Church has promised to flood money into the state to support "public education" on the issue.</P>
<P>Second, Bale argues that support for gay marriage "shows how far [gay rights groups] have drifted from groups like the Gay Liberation Front (GLF)." Clearly, we do not see mass revolutionary movements like those of the 1960s and '70s today. </P>
<P>But the GLF was part of--and a product of--the massive shift in consciousness toward revolutionary politics in the 1960s. However, the GLF was an organization with a middle class base that did not have an orientation to, nor an analysis of class politics. </P>
<P>Gay marriage is a class issue that should naturally be part of any fight for civil rights and equality for gays and lesbians--an issue that socialists would have supported in the 1960s.</P>
<P>Rather than being "long considered a fringe concern," gay marriage has in fact been an automatic demand of many who support gay rights. In June 1970, for example, on the one-year anniversary of the Stonewall rebellion in New York City, the Los Angeles county clerk's office was flooded with requests from gay and lesbian couples applying for marriage licenses. </P>
<P>As socialists we also argue that the road toward the revolutionary transformation of society must be lined with battles for reforms. Gay marriage is such a reform, and one that we should unconditionally defend. It is also exactly the type of issue that gives confidence to our side because, as the Massachusetts decision shows, it is a reform that we can win.</P>
<P>What socialists should say is that marriage is a legal right that grants domestic benefits--like health care and hospital visitation rights. It is also a public declaration of love and commitment--something that, as gays and lesbians, we have always been denied. </P>
<P>Despite 50 percent of marriages falling apart and despite the violence that takes place within homes, we support people's right to marry. Reforms like the recent SJC ruling cut through the hypocrisy of bourgeois "family values" as well as break down divisions within the working class. </P>
<P>Socialists also defend the right to divorce--we do not abstain from taking positions on social relationships while we strive toward socialist revolution. Marriage is undoubtedly an institution within bourgeois society, however it is a reform, and one that tangibly benefits the majority of gays and lesbians--those in the working class.</P>
<P>Lastly, Bale's argument against the Democrats--who oppose gay marriage but support civil unions--is nullified by his own dismissiveness about the victory. How can socialists belittle gay marriage yet condemn Democrats for not supporting it? </P>
<P>As socialists we should celebrate and defend this victory and use it as a beacon to both win others away from the Democratic Party, as well as to show how much support for gay rights there is that can be organized into a movement in the streets. </P>
<P>Many people here have had friends and co-workers get engaged and even come out since this ruling. People all over the state, both straight and gay, will be attending gay weddings in a few short months. The confidence that has been gained by our side in the immediate aftermath of this decision and the serious blow levied against discrimination and bigotry cannot be understated. </P>
<B><P>Alpana Mehta, Jeff Offermann and Steve Trussell , </B>Boston</P>




