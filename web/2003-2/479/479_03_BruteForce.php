<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Bush's "war on terrorism" exposed for what it really is...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The rule of brute force</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 12, 2003 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ASKED TO explain the overwhelming and savage force used by the U.S. during the Vietnam War, Gen. William Westmoreland told an interviewer: "The Oriental doesn't put the same high price on life as does the Westerner." To antiwar activists around the world, Westmoreland's statement summed up the racism and barbarism of the U.S. slaughter of some 2 million in Southeast Asia--a war that also cost the lives of 58,000 U.S. soldiers.</P>
<P>But to Washington, it was all justified in the name of stopping "communism." Now comes Capt. Todd Brown of the Fourth Infantry Division in Iraq, using Westmoreland's racist logic to justify harsh new U.S. tactics in Iraq--including imprisoning entire villages behind barbed wire and demolishing civilians' houses.</P>
<P>"You have to understand the Arab mind," he said. "The only thing they understand is force--force, pride and saving face." Brown is one of the U.S. commanders who admit to adopting methods used by the Israeli government to crush and humiliate the Palestinian population in the West Bank and Gaza.</P>
<P>Measures necessary, they claim, to fight "terrorism." Racism has long been a preferred tool of warmakers, of course, helping to dehumanize the "enemy" and justify killing as many as possible. </P>
<P>The "war on terrorism" is no different. Yet "terrorism" isn't used only to justify atrocities in Iraq, but to further U.S. military interventions abroad. That's the essence of the "Bush Doctrine" of pre-emptive wars, announced during the buildup for the invasion of Iraq.</P>
<P>While the world's attention has been focused on the occupation of Iraq, U.S. military forces continue to fight in Afghanistan. A U.S. A-10 warplane carried out an attack in southeastern Afghanistan December 6 to try to assassinate a "known terrorist." Nine children were killed instead.</P>
<P>But don't expect to hear supposedly antiwar Democratic presidential candidates like Howard Dean object. According to Dean, the problem with the Bush administration war makers is that they "are bogged down in Iraq, they are not defending us from Osama bin Laden, and they are not paying any attention to Latin America, which is the most important hemisphere in American history."</P>
<P>In fact, U.S. military "trainers" <I>are </I>at work in Latin America--helping the Colombian military carry out its dirty war against rebels with new military helicopters, an intervention escalated under former president Bill Clinton's "Plan Colombia." There are many other examples of U.S. military intervention justified by the "war on terrorism"--from U.S. troops posted in distant Central Asian republics of the former USSR, to the Philippines, where the U.S. was able to use the September 11 attacks to rebuild a military presence.</P>
<P>At home, the "war on terror" continues to be an all-purpose tool for gutting civil liberties, with Arabs and Muslims the main target of the repressive USA PATRIOT Act. The recent expansion of FBI powers in a closed session of Congress highlights how terrorism is still being used to give police-state powers to law enforcement agencies.</P>
<P>That's why demanding an end to the occupation of Iraq, important as it is, isn't enough. We need to build a movement that can expose the "war on terrorism"--on all its fronts--for what it really it is: a blank check for imperialist aggression abroad and a means to suppress dissent at home.</P>



