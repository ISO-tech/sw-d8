<P><font face="Times New Roman, Times, serif" size="4">Grim reality behind the politicians' photo ops</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Hunger in the richest nation in the world</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Adam Meyer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 12, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE BUSH administration's "war on terror" is justified with language about "national security." But what about the security of having access to basic necessities and ensuring that needs of children are met? Bush says, "Bah humbug!"</P>
<P>Last month, the Children's Defense Fund noted in a report that 13.1 million children in the U.S. live in households that are classified as "food insecure" by the U.S. Department of Agriculture. That means they either suffer regularly from hunger, or they or their parents have had to skip meals or experienced difficulties in getting enough to eat. </P>
<P>In Texas, where Bush "governed" for six years--and the home to some of the country's wealthiest corporations, which have profited enormously from the invasion and occupation of Iraq--it's estimated that 1.1 million young children in the state are hungry or at risk of hunger. That's one in three kids in Texas.</P>
<P>Currently, 34 million people suffer from hunger in the U.S. alone, and for millions more, the holiday season is a difficult time--one of hard decisions about how to make ends meet. And the problem is getting worse. A survey by the U.S. Conference of Mayors found that requests for emergency food assistance jumped by nearly 20 percent in 2002.</P>
<P>The money exists to end hunger in the U.S. overnight. But politicians like Bush are determined to spend it in pursuit of corporate interests across the U.S. and around the world--when they aren't lining the pockets of the already super-rich with more tax cuts.</P>
<P>Remember that the next that Bush or some other politician is posing at a food bank or a homeless shelter to prove how much they care. Their actions are the reason that hunger still exists in the richest country in the world.</P>



