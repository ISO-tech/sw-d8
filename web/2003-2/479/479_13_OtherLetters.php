<p><a name="Georgia"></a><font face="Times New Roman, Times, serif" size="4"><b>Why the U.S. backs revolt in Georgia</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</I> <BR>
</B>Elizabeth Schulte's report on last month's revolution in Georgia ("Mass protests topple U.S. ally," <I>SW,</I> December 5) fails to mention one important dimension of what happened. According to the Toronto <I>Globe and Mail,</I> ousted President Eduard Shevardnadze had recently signed a secret agreement with Russia's Gazprom energy company, giving it a monopoly on gas imports to Georgia. He also gave control of the electricity grid to another Russian firm, rejecting the bid of a U.S.-backed company.</P>
<P>Shevardnadze had been regarded as a Western ally for years. An envoy from the Bush administration warned him that "Georgia should not do anything that undercuts the powerful promise of an East-West energy corridor," which would allow oil and gas from the Caspian Sea region to be piped through Georgia to Western Europe.</P>
<P>When the energy deals with Moscow went ahead, Shevardnadze's fate was sealed. If Shevardnadze had played ball with Washington, Georgia's rigged November 2 election would have been ignored. Instead, U.S.-backed organizations exposed the election fraud and funneled money and resources to opposition groups.</P>
<P>The Bush administration may yet come to regret the popular movement it helped to unleash in Georgia, but Britain's <I>Guardian</I> newspaper was not far off the mark in calling Shevardnadze's downfall "a U.S.-engineered coup."</P>
<B><P>Phil Gasper, </B>Berkeley, Calif.</P>

