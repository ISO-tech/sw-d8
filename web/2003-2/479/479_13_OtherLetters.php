<p><a name="Health"></a><font face="Times New Roman, Times, serif" size="4"><b>We need national health care</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</I>,</B> <BR>
A recent study by the health care consulting firm the Lewin Group projected that Maryland Citizen's Health Initiative plan to increase coverage to the uninsured in Maryland's would not cause job losses. Evidence from Canada indicates that having a health care system that provides for all and not for the profit of the insurance and drug industries has contributed to an increase in manufacturing jobs there, especially in the auto industry.</P>
<P>U.S. auto companies continue to relocate north of the border, taking advantage of Canada's national health insurance system that makes health care costs much lower than in the U.S. The problem here, of course, are the private insurance and drug companies who have unimaginably high stakes in the for-profit health care system. </P>
<P>And they've demonstrated that they're ready to do whatever it takes to preserve their privileges. One startling fact: there are now more highly paid lobbyists for the drug companies than there are members of Congress.</P>
<B><P>Michael Melick, </B>Baltimore</P>



