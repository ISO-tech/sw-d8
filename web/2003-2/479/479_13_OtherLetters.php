<P><p><font face="Times New Roman, Times, serif" size="4"><b>Keep recruiters off campus</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Myself and about seven other students at Seattle Central Community College (SCCC) have formed a chapter of the Campus Antiwar Network this quarter. We are organizing a speakout on campus as part of an international student day of protest--with our two main demands being to end the occupation and to get military recruiters off our campus. In addition to launching a petition for students and staff to sign, we'll be marching to the administration office at the end of the protest to present a letter demanding that the military be banned from campus. </P>
<P>Several times this quarter, we have set up antiwar tables right next to Navy recruiters in an effort to let them know we don't want them on campus. SCCC used to post the times and dates of when the recruiters would be on campus, but since we began tabling, they no longer publicly announce when the recruiters will be coming. </P>
<P>We consider the fact the military can't now openly say when they're coming to campus to be a small victory for us. We also challenged the Navy recruiter to a debate on "Should students support the occupation and/or should they join the military?" We've yet to get their response. </P>
<P>While it hasn't been easy, and we have lots of work still to do, I feel like our experience shows that even in these tough times a small group of determined activists can make a big difference on a campus--or anywhere else, for that matter. </P>
<B><P>Darrin Hoop, </B>Seattle</P>

