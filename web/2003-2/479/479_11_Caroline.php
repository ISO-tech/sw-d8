<P><font face="Times New Roman, Times, serif" size="5"><b>Race and inequality in a Southern home</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Jessica Carmona-Baez</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 12, 2003 | Page 11</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2"><B><I>Caroline, Or Change, </B></I>lyrics and script by Tony Kushner, music by Jeanine Tesori. Playing at New York City's Public Theater through January 4.</font></P>

<P><font face="Times New Roman, Times, serif" size="3"><I>CAROLINE, OR Change </I>is a wonderful new musical by Pulitzer Prize-winning playwright Tony Kushner of <I>Angels in America </I>fame. It takes place in Louisiana in 1963 and tells the story of Caroline Thibodeaux, a Black housekeeper for a wealthy Jewish family, the Gellmans.</P>
<P>Caroline spends her days in the dingy basement of the Gellmans' lovely Southern home. Through gut-wrenching Black spirituals, <I>Caroline</I> expresses the desperation of working for $30 a week amid abundant wealth.</P>
<P>The story also follows the Gellmans' 9-year-old son, Noah, who grows attached to Caroline after his mother dies from cancer. Change is a theme that runs through the entire play. Noah repeatedly leaves change in his pockets for Caroline, and secretly checks to see if Caroline takes it.</P>
<P>For the first half of the play, Caroline doesn't take the change, but collects it in a bleach cup on top of the washing machine for Noah to take back. This symbolizes Caroline's resistance to the social change happening around her.</P>
<P>Though Caroline is filled with anguish about her social position, she is at the same time reluctant and fearful of change. This reluctance is also shown through her conflict with her rebellious daughter, Emmie, who, swept up by the 1960s spirit of struggle, is frustrated by what she sees as her mother's complacency and subservience to the establishment.</P>
<P>The music is what makes <I>Caroline</I> a great play, but the set designer deserves a lot of credit too. The stark contrast between the lonely basement laundry room and the finely decorated Gellman house captures the amazing inequality between the wealthy family and the people who work for them.</P>
<P>For theatergoers, this play is a welcome relief from typical Broadway musicals, which are so often filled with unrealistic characters and showy dance numbers. For activists, it is a powerful reminder of why we are committed to fighting for a world free of racism and inequality.</P>

