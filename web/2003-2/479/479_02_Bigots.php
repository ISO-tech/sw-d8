<P><font face="Times New Roman, Times, serif" size="5"><b>The sick bigots in a Louisiana school</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 12, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">LOUISIANA SCHOOL administrators gave new meaning to the word "bigot" last month when they punished a 7-year-old boy for using the word "gay." Second-grader Marcus McLaurin was waiting for recess on November 11 at his Lafayette elementary school when a classmate asked about his parents. Marcus said that his mothers were gay, explaining that "gay is when a girl likes another girl."</P>
<P>Marcus' teachers immediately told Marcus that "gay" was a "bad word"--then marched him to the principal's office, where he was told to write "I will never use the word 'gay' in school again" as punishment. Despite efforts by the American Civil Liberties Union to have the incident removed from Marcus' file, the school has refused to even apologize to the boy or his family.</P>
<P>"I was concerned when the assistant principal called and told me my son had said a word so bad that he didn't want to repeat it over the phone," said one of Marcus' mothers, Sharon Huff. "But that was nothing compared to the shock I felt when my little boy came home and told me that his teacher had told him his family is a dirty word."</P>



