<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="4">The growing ranks of the poor aren't celebrating</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Happy holidays for the few</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 12, 2003 | Page 4</font></P>

<P><font face="Times New Roman, Times, serif" size="3">After sitting out the last holiday season, affluent shoppers have come roaring back--snatching up $1,200 diamond-studded watches and clearing $710 pink quilted Prada bags off the shelves at Saks Fifth Avenue. But while the rich are stocking up on "anything pink," according to an elated Saks executive, an ever-wider swathe of Americans will be lucky to afford anything at all this holiday season.</P>
<P>According to a report by the Consumer Federation of America, 34 percent of those surveyed plan to spend less, not more this year. That means lining up at 6 a.m. like a herd of cattle outside discount retailers like Wal-Mart, in the hopes of grabbing a low-priced "door buster" before it sells out.</P>
<P>The media had a field day with the story of a Florida woman trampled by a horde of shoppers lunging toward a stack of $29 DVD players when the 6 a.m. buzzer sounded at an Orange City Wal-Mart the day after Thanksgiving. Newspaper editorials chastised greedy shoppers for the incident, in which paramedics had to fight their way through the Wal-Mart mob to find the woman lying unconscious on top of a DVD player.</P>
<P>Reporters could have pointed out--but didn't--that a regularly priced DVD player is out of reach for most workers in Florida, where the median hourly wage is $9.90--5.4 percent below the national average. The trampled shopper herself, who was buying the DVD player as a present for her mom, is a former employee of Wal-Mart, where the average wage is $7.50. </P>
<P>The only greedy party in the entire incident, in fact, was Wal-Mart, which could only muster enough good will to offer to put the DVD player on hold for later purchase by the hospitalized woman.</P>
<P>Economists busy clucking about the surging gross domestic product and the rallying stock market have paid little attention to the most unique dynamic of the present recovery--skyrocketing corporate profits combined with sharply falling wages and benefits. Two years into this recovery, according to the Economic Policy Institute, roughly 29 percent of corporate incomes went to labor--half the typical rate of 61 percent at this stage of previous recoveries.</P>
<P>The money is instead flowing upward, with 46 percent going to profits, compared with 32 percent normally. And Bush's tax cuts hand another $93,500 to each millionaire, while the roughly 3 million workers who have lost their jobs since the recession began have long since run out of unemployment benefits and are lining up for food handouts.</P>
<P>Last month alone, 17,000 more factory workers lost their jobs--the 39th consecutive month of manufacturing job decline. In Ohio, two million of the state's 11 million people had to turn to charity to put food on the table last year.</P>
<P>In the industrial town of Logan, a line of cars stretching back half a mile forms at dawn for food handouts that don't begin until 9 a.m. The U.S. Department of Agriculture reports that 1.6 million New Yorkers--equivalent to the population of Philadelphia--suffer from "food insecurity" (otherwise known as "hunger"), as do over 31 million people nationally.</P>
<P>According to official government statistics, 34.6 million Americans are living in poverty, over 13 million of them children--one out of eight people. But the government measure, which does not include necessities such as childcare costs into its calculation, vastly underestimates the swelling ranks of the poor.</P>
<P>The National Academy of Sciences estimates that the poverty threshold would be as much as 45 percent higher if accurately measured--raising the poverty rate by three percentage points, or nine million more people. A record 1.6 million people filed for personal bankruptcy in the last year--double the number a decade ago--the vast majority driven into debt by job loss and medical bills, including a growing number of single mothers paying for necessities with credit cards.</P>
<P>As <I>Newsweek</I> columnist Anna Quindlen commented last week, "The American Dream for the well-to-do grows from the bowed backs of the working poor, who too often have to choose between groceries and rent."</P>



