<P><font face="Times New Roman, Times, serif" size="4">Teamsters boost grocery strike as bosses play dirty</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Time to step up the fight</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Karl Swinehart and Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 12, 2003 | Page 15</font></P>

<P><font face="Times New Roman, Times, serif" size="3">LOS ANGELES--The strike and lockout of 70,000 grocery workers in Southern California entered a critical phase in mid-December. The grocery industry giants are losing big money after underestimating workers' willingess to fight--and the solidarity they've received.</P>
<P>The workers--members of the United Food and Commercial Workers union (UFCW)-- are resisting concessions that would price health care out of reach of workers  and impose a two-tier pay system for new hires.</P>
<P>The outcome of this struggle will affect unions across the U.S. "If we go under, everyone else is going to follow suit," said striker Kathleen Doyle, who's worked for 17 years at the Vons in the Echo Park area of LA.</P>
<P>The strike got a major boost just before Thanksgiving, when the International Brotherhood of Teamsters agreed to honor picket lines by the United Food and Commercial Workers union (UFCW) at 10 distribution centers that supply the grocery chains. Teamsters President James P. Hoffa announced that the union would pay the 8,000 workers affected $200 per week.</P>
<P>The move represents a "rebirth of labor solidarity" Harley Shaiken, a labor export at the University of California-Berkeley, wrote in the <I>Los Angeles Times.</I> "In the midst of a knock-down, drag-out economic struggle, the cooperation between these two unions could breathe new life into organized labor and transform the way strikes are waged."  </P>
<P>The Teamsters' action comes after weeks of demonstrations and pickets involving unions across the region--including a dramatic "stop work" rally November 10 by the International Longshore and Warehouse Union. Miguel Contreras, Executive Secretary-Treasurer of the Los Angeles County Federation of Labor, has played a crucial role in mobilizing solidarity. He recognizes that if the employers prevail, it would undermine more than a decade of organizing by LA unions among low-paid and immigrant workers.</P>
<P>The confrontation began October 12, when the UFCW walked off the job at Vons and Pavilion stores, which are owned by Safeway Inc., the driving force on management's side. The following day, Alberstons Inc. and Kroger Co.'s Ralphs stores locked out the union.</P>
<P>Yet UFCW leaders made a huge strategic blunder in late October, pulling the picket lines from Ralphs--even though Kroger had already forced the UFCW on another strike in West Virginia. The strategy, according to union insiders, was to curry favor with the public while trying to sow division among the companies.</P>
<P>Instead, the companies agreed to a "mutual aid pact" to share profits--and are now under investigation by the California state attorney general for antitrust violations. Yet the UFCW apparently has no plans to resume pickets at Ralphs.</P>
<P>Albertsons reported a 51 percent drop in profits for the third quarter, which included the strike, and Safeway and Ralphs are also expected to have losses. With Christmas approaching, management is under increased pressure to settle--but striking and locked-out workers are also under enormous financial stress. Now is the time to raise the stakes by returning picket lines to Ralphs--and mobilizing even more labor solidarity to boost this crucial fight.</P>



