<P><font face="Times New Roman, Times, serif" size="5"><b>Chaplain smeared by the Pentagon</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 12, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEVENTY-SIX days. That's how long Army Chaplain Yousef Yee spent locked in a Navy brig, smeared by the Bush administration with the accusation of espionage.</P>
<P>Capt. Yee served as a spiritual advisor and interpreter at Camp Delta, in Guant&aacute;namo Bay, Cuba, where hundreds of prisoners of the U.S. "war on terror" are housed. In September, the Pentagon arrested Yee, claiming that he was carrying improper information about prisoners. </P>
<P>Yee supposedly was found carrying sketches of Camp Delta, and several "anonymous law enforcement officials" were quoted in the press saying that he was suspected of everything from spying to "aiding the enemy" to treason. For more than two months, Yee was jailed in a maximum-security Navy lockup in Jacksonville, Fla., where he was only let outside his cell in shackles for one hour each day.</P>
<P>The government zeroed in on Yee in particular because he received religious instruction in Syria--which the Bush administration has repeatedly accused of aiding and harboring terrorists. But when the government couldn't make an espionage case against Yee stick, they charged him in October with "disobeying a general order"--for improperly handling classified material.</P>
<P>To add insult to injury, when Yee finally was released from the brig on November 19, the government added charges of adultery, making a false statement and downloading pornography on his government computer. This latest round of ridiculous charges is an attempt for the Pentagon to save face by charging Yee with <I>something</I>--after having whipped up a media frenzy over a suspected "spy" at Camp Delta. It's not surprising, considering that the Pentagon is also in the process of trying to make similar charges stick against two military translators and an intelligence officer who also worked at Camp Delta.</P>
<P>Don't expect an apology from the government for destroying Yee's reputation. A spokesman for the U.S. Southern Command, Navy Lt. Commander Chris Loundermon, told the <I>Boston Globe</I> that the military has no control over what "anonymous" sources choose to tell a reporter. </P>
<P>"No [Department of Defense] official has said that Captain Yee and espionage were linked together," Loundermon said. "As for unnamed government officials, unfortunately, there are lots of people out there who have their opinions on it, but they're unnamed, and they're not giving an official statement." In other words: they can get away with any lie that they want to--as long as it's anonymous. </P>



