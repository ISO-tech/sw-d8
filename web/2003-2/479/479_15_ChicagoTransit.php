<P><font face="Times New Roman, Times, serif" size="5"><b>Bad deal forced on Chicago transit workers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lauren Fleer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 12, 2003 | Page 15</font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--Nearly 6,300 Chicago Transit Authority (CTA) workers, members of Amalgamated Transit Union (ATU) Local 241, have a new contract--and they're not happy about it. On November 21, an arbitrator decided the terms of a long-awaited contract for the bus operators, bus repairmen, bus servicers, and warehouse, treasury and office administrative staff.</P>
<P>"The contract sucks," one mechanic told <I>Socialist Worker</I>. "It's the same contract we didn't want four years ago." While the contract does include a $3 an hour pay raise, retroactive to January 1, 2000, workers are largely dissatisfied with the deal and with the performance of union leaders.</P>
<P>In February of this year, the ATU International took over management of Local 241, whose president was sued by the executive board. Workers are also frustrated with their lack of involvement in the local, which hasn't called a meeting in over three months.  </P>
<P>The transit workers' last contract expired December 31, 1999. In the four years since, the CTA settled contracts with 16 other union locals, whose members it employs.  </P>
<P>During the four-year stalemate, the Local 241 leadership negotiated two agreements that the members voted down. This August, both union and management brass agreed to settle the dispute through binding arbitration.</P>
<P>Because the union surrendered the members' right to vote, the workers are now forced to accept the terms of the recent deal, which makes work-rule concessions and increases the number of part-time bus drivers to 25 percent of the workforce, up from 20 percent.</P>
<P>Ironically, the contract settled by arbitration will expire in just two weeks. When the next bargaining sessions begin, rank and filers will have a new chance to get organized and raise their voices for a better deal.</P>


