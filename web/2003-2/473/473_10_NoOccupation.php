<P><font face="Times New Roman, Times, serif" size="5"><b>No to the occupation of Iraq!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 24, 2003 | Page 10

<P><font face="Times New Roman, Times, serif" size="3">ACTIVISTS AROUND the country are saying no to the war and occupation in Iraq at "Speaking Truth to Empire" meetings sponsored by the Campus Antiwar Network (CAN). Some 500 people turned out at Columbia University in New York City October 14 to hear Amy Goodman of the radio show <I>Democracy Now,</I> Trish Shaft from Military Families Speak Out (MFSO) and activist and author Tariq Ali.</P>
<P>Earlier that day, 150 students packed the Pace University auditorium to hear a panel cosponsored by the Arab Alliance at Pace. Simon Harak of the War Resisters League, Sara Flounders of International ANSWER, Barbara Nimri Aziz of Pacifica Radio and Anthony Arnove, editor of <I>Iraq Under Siege</I>, spoke.</P>
<P>In California, the Berkeley Stop the War Coalition hosted the opening night of the tour October 8, where 200 people came out to hear left-wing columnist Norman Solomon, author Michael Parenti and Jim Bronke of MFSO. Afterward, more than 50 students signed up to get involved with the coalition, and even more said they were coming to the October 25 protest in San Francisco, which coincides with another national demonstration in Washington.</P>
<P>The next week, another 150 people turned out for the tour's stop at San Francisco State University. Speakers included Medea Benjamin of Code Pink, Eyad Kishawi of the Free Palestine Alliance and Katrina Yeaw from SF State's Students Against the War.</P>
<P>About 100 people came to a meeting at the University of Wisconsin in Madison, which featured speakers Lou Plummer from MFSO and Will Williams from Veterans for Peace. A veteran and the father of an enlisted soldier, Plummer noted that "not long ago, our troops in Iraq were worrying about algebra tests and prom. These same young men and women are now concerned with health insurance and whether or not to pull the trigger at the truck coming down the road." Panelist John Farrell from the Voices in the Wilderness spoke about the devastating impact of United Nations sanctions on the people of Iraq.</P>
<P>The tour made two stops in Chicago at the University of Illinois and Northeastern Illinois University on October 16. More than 60 people turned out at UIC to hear speakers who included Pearlie Stuckley, the mother of a soldier in Iraq, and Vietnam Veterans Against the War's Bill Davis. "I defy anyone who says we are making life better for the Iraqi people when the U.S. is denying them everything, including their lives," said Davis.</P>
<P>Concerning the administration's attacks on Muslims and people of Middle Eastern descent, Sadia from the Muslim Student Association said, "It is nothing more than racism; we cannot bring them democracy...we need democracy here!" At Northeastern, 35 people attended. Speakers linked the conditions for U.S. troops in Iraq with those on campus, where working-class students are frequently targeted by military recruiters.</P>

<B><P>Justice for Private Funk</P>
</B><P>On October 18, International ANSWER sponsored a talk by Gloria Pacis, the mother of Stephen Funk, a Marine private who is in a military prison. His ostensible crime was going AWOL, but he is really being punished for speaking out against the war in Iraq.</P>
<P>Funk, who has attended antiwar rallies, applied for conscientious objector status, but was turned down. He went AWOL for 47 days and then turned himself in. "Our chance to stand with Steve comes on October 25," Pacis said.</P>

<B><P>End the detentions!</P>
</B><P>Students from Rochester Institute of Technology Antiwar cosponsored a protest October 11 at an immigrant detention center in Batavia, N.Y. About two dozen people attended. "The attacks on immigrants' rights are a different front of the same war," protester Josh Ganger told <I>Socialist Worker. </I>"Bush's policies are all connected."</P>
<I>
<P>Nathan Fuller, Evan Kornfeld, Peter Lamphere, Khury Petersen-Smith, Justine Prado, Jessica Preston, Suzie Schwartz, Michael Smith, Adam Turl and Chris Williams contributed to this report.</P>
