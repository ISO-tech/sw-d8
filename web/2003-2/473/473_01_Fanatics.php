<P><font face="Times New Roman, Times, serif" size="4">"George Bush was appointed by God"</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>These fanatics want to rule the world</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 24, 2003 | Page 1</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"GEORGE BUSH was not elected by a majority of the voters in the United States. He was appointed by God." At least Lt. Gen. William Boykin, the Bush administration's brand new deputy undersecretary of defense for "intelligence," got the first part right. But his claim that God was directly responsible for putting Bush in the White House "for a time such as this" is further evidence--if any more was needed--that the White House is packed with arrogant and power-hungry fanatics.</P>
<P>If Boykin were Arab, you can bet that he would have been dismissed as a "religious extremist." Instead, when asked about Boykin's repeated fundamentalist ramblings, Defense Secretary Donald Rumsfeld hailed him as a general with an "outstanding record." It was a typical display of the Bush administration's stunning arrogance--arrogance that is leading more and more people to question the war and occupation of Iraq.</P>
<P>Like Specialist Juan Castillo, who is stationed in Iraq. "In the beginning, I was into this; we all were," Castillo told the <I>New York Times </I>on a recent furlough home.</P>
<P>But now, he says thinks the war is a waste--and says that the Iraqis want the U.S. out. "We're conquerors to them," he said. "It wasn't supposed to be like that." For Iraqis subjected to constant humiliation and brutality at the hands of U.S. forces, the occupation will only continue to inspire more rage--and resistance.</P>
<P>It's easy to see that William Boykin is a fanatic. But there are plenty more fanatics in Washington. They're bent on waging "pre-emptive" wars. They're willing to trample on civil liberties to silence dissent at home. They're determined to spend billions in their drive for oil profits and world domination--while calling it "liberation."</P>
<P>That's why it's more important than ever to stand up against the Washington war makers. Activists should get on the bus for the October 25 demonstrations in San Francisco and Washington, D.C. End the occupation! Bring the troops home now!</P>



