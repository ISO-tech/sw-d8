<P><font face="Times New Roman, Times, serif" size="5"><b>Lives destroyed by the injustice system</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 24, 2003 | Page 4

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,<br>
</B></I>A 2-year-old girl in Jacksonville, Fla., was recently rescued after being left alone in an apartment for two-and-a-half weeks. The child, found covered in filth and wrapped in a towel, had survived by eating ketchup, jelly and dry pasta. </P>
<P>How could something so horrible as this happen? Because her 22-year-old mother, Dakeysha Lee, was arrested and thrown in jail for petty theft and assault. </P>
<P>Reading the report, I immediately thought of a similar incident last March, when a young mother was jailed in the southern Chinese city of Guangzhou, leaving her 3-year-old to starve to death in her apartment. The Chinese incident sparked international outrage and forced the firing of several police directors. </P>
<P>But don't expect heads to roll in Jacksonville. Police and prison officials, desperate to place the blame on the young mother, have seized custody of the child and are bringing charges against the mother for child abuse.</P>
<P>This is an outrage! Even in the unlikely event that the mother failed to tell the appropriate officials about her child, that is still no excuse. It is the politicians--who have built up a system that routinely tosses poor mothers into jail--who are truly responsible for the wellbeing of the children and other dependents affected. </P>
<P>And, it is those politicians who have blood on their hands for the lives destroyed by incarcerating hundreds of thousands. We must organize to put an end to the racist criminal injustice system--not just for ourselves, but to protect our children and loved ones.</P>

<B><P>Mike Stark, </B>Washington, D.C.</P>

