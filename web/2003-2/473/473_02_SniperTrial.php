<font face="Times New Roman, Times, serif" size="5"><b>Media circus for sniper trial</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mike Stark</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 24, 2003 | Page 2</P>

<P><font face="Times New Roman, Times, serif" size="3">AFTER MONTHS of press conferences, book deals, talk show tours, and shameless maneuvering by prosecutors and police, the trial of sniper suspect John Allen Muhammad has begun. But don't expect the truth about last year's sniper killings in the Washington, D.C., area to come out.</P>
<P>From the moment of the initial arrest of Muhammad and juvenile suspect Lee Malvo, this case has been about nothing but politics and scapegoating. From the start, Attorney General John Ashcroft was used the case for his own agenda. He personally intervened to make sure that Muhammad and Malvo were removed from custody in Maryland and handed over to Virginia prosecutors--to make the death penalty more likely.</P>
<P>Virginia is second only to Texas in its number of executions--and it is one of 17 states in the nation that allows for the execution of 16-year-olds. Ashcroft can't be disappointed. Muhammad's prosecutor is Prince William County Commonwealth's Attorney Paul Ebert, who has put 12 men on death row, more than any other prosecutor in Virginia. </P>
<P>Malvo will be prosecuted by Fairfax County Commonwealth's Attorney Robert Horan Jr., who is best-known for getting a death sentence against a mentally ill man who was accused of killing two CIA employees outside the agency's headquarters. In Muhammad's case, Ebert is trying out a new anti-terrorism law--in the hope of overcoming a "limitation" in Virginia's death penalty statute that requires a defendant to be the "triggerman" in order to be eligible for death. </P>
<P>The new law, passed soon after the September 11 attacks, allows the death penalty for those who murder in order to "intimidate the public." No eyewitnesses or confessions link Muhammad or Malvo to the shootings, and the evidence against them is circumstantial. Muhammad's backup lawyer, Peter Greenspun, said in court, "We don't know how they're going to prove [Muhammad's guilt], because there is no evidence."</P>
<P>What's worse, prosecutors plan to say two contradictory things in the different trials of Muhammad and Malvo. To show that Muhammad was "the captain of the killing team," Paul Ebert will argue that he controlled all of Malvo's actions. But in Malvo's case, Robert Horan will say that the teen acted of his own free will.</P>
<P>Since the shootings, prosecutors and police from Maryland and Virginia have been jumping in front of the cameras and writing "eyewitness" accounts. But nowhere is anyone asking why the shooting spree happened.</P>
<P>If he is guilty, Muhammad is part of a growing list of veterans of the 1991 Gulf War who have been accused of horrendous crimes. Experts believe that Muhammad's alleged role in the sniper shootings would fit the pattern of a veteran suffering from Gulf War Syndrome. And in fact, Muhammad was a member of the U.S. Army's 84th Engineer Company and helped to demolish an Iraqi ammunition dump containing rockets filled with the nerve agent sarin. </P>
<P>The Pentagon later admitted that the process of blowing up the rocketsmay have vaporized dangerous amounts of sarin. "When he got back," Muhammad's ex-wife told the <I>Washington Post, </I>"he was a very angry man. I didn't know this man. The one I knew stayed in Saudi." </P>
<P>Dr. William Baumzweiger, a Los Angeles neurologist and psychiatrist who specializes in treating those suffering from the mysterious collection of illnesses known as Gulf War Syndrome, recognizes this profile. "Once it came out that he had a military background, I said this must be a Gulf War veteran," he told MSNBC. "There is no doubt that a small but significant number of Gulf War veterans became homicidal because of Gulf War Syndrome."</P>
<P>These prosecutions--and the media hype surrounding Muhammad's trial--make a mockery of justice.</P>



