<P><font face="Times New Roman, Times, serif" size="5"><b>Grocery workers digging in for a long fight</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Evan Kornfeld and Gillian Russom</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 24, 2003 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">LOS ANGELES--The 70,000 striking grocery store workers in Southern California are gaining widespread sympathy throughout the area. Some 2,000 union members from around Southern California rallied at a Pavillion's grocery store (owned by Von's) at Melrose and Vine in LA.</P>
<P>Teamsters have refused to cross picket lines, forcing managers to drive trucks to complete the deliveries. "I've been overwhelmed with all the support we've gotten on the picket line," Marco, one of the picket captains at the Von's store in LA's Echo Park neighborhood, told <I>Socialist Worker</I>.</P>
<P>"Teachers have come by; the steelworkers, the carpenters' union, the janitors."</P>
<P>Labor groups have been holding rallies at stores throughout Southern California to show their support for the strikers. Rev. Jesse Jackson spoke to a rally in Santa Monica October 18 and was set to speak at a Ralph's on Crenshaw Boulevard in Los Angeles the following day.</P>
<P>A strike by workers at Von's (owned by Safeway) October 11 led to a lockout at two other chains--Ralph's (owned by Kroger) and Albertson's. The UFCW has filed a lawsuit against Ralph's and Albertson's, claiming that the two chains violated a law requiring that each employee be given 60 days notice prior to a mass layoff.</P>
<P>These huge grocery chains--all of them profitable--are demanding cuts in health care and pension funds, as well as demanding a two-tier wage system. One of the main concerns of the strikers is the companies&#130;proposals for the pension plan.</P>
<P>Under the current contract, workers cannot receive full retirement benefits until their age plus their years of service with the company adds up to 85. However, part-time workers are not awarded a full year of service towards their retirement unless they work 1,800 hours in a year, which is almost impossible. In negotiations for the next contract, Von's is demanding that the retirement number be increased to 95--which means that in order to retire at 60, a worker would have to have worked for 35 years.</P>
<P>The mood at the picket lines has generally been upbeat, and workers are confident. Most customers have been refusing to cross the picket lines and sales are down at all three chains.</P>

<p><font face="Times New Roman, Times, serif" size="4"><b>Midwest grocery workers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">ANOTHER 13,000 grocery workers are on strike in St. Louis and in West Virginia, Ohio and Kentucky. In St. Louis, workers--also members of the United Food and Commercial Workers (UFCW) Local 655--walked off the job October 7 at the Shop 'n Save chain, which is owned by the SuperValue store.</P>
<P>Two other locally owned grocery chains, Dierberg's and Schnuck's, locked out the union immediately afterward, putting 10,000 workers on the picket line. The issues are similar to those in the big Southern California grocery strike--a wage freeze with a small bonus instead, outsourcing jobs to nonunion vendors, cuts in early retiree benefits and major givebacks on health care.</P>
<P>As in California, business has fallen off drastically, and customers and other unions have shown support for the strike. UFCW Local 400 went on strike October 13 at a chain of 37 Kroger stores centered in West Virginia. Kroger--owner of the strike-bound Ralph's chain in California--wants to dramatically reduce funding for workers' health insurance coverage, paying only $9 million into a fund which, according to the union, needs $29 million.</P>



