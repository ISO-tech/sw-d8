<P><font face="Times New Roman, Times, serif" size="4">A general's indictment of U.S. imperialism</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"To hell with war"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Ashley Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 24, 2003 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">Smedley Butler, <B><I>War Is a Racket. </B></I>Feral Press, 2003, 80 pages, $9.95.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"I SPENT 33 years in the Marines, most of my time being a high-class muscle man for big business, for Wall Street and the bankers. In short, I was a racketeer for capitalism." This is how one the most decorated soldiers in U.S. history, Gen. Smedley Butler, summarized his career in the U.S. military during the early years of the 20th century.</P>
<P>Butler was not a minor figure. He led American troops in attacks and occupations throughout Latin America, the Caribbean and Asia. In fact, his reputation was so gruesome that mothers in Nicaragua would discipline their children by saying that if they disobeyed, General Butler--not Satan--would get them.</P>
<P>Butler turned against the U.S. military after retiring from the Marines. Reflecting on the horrors of the First World War and the U.S. government abandonment of its veterans during the Great Depression, he began to see how the U.S. used the military to plunder the world and in the process sacrificed the lives of working-class soldiers.</P>
<P>Butler's 1935 classic <I>War Is a Racket</I> has just been reissued by Feral House Books<I>.</I> In this short book, Butler lays out his indictment of American militarism. "War is racket. It has always been," Butler wrote. "It is possibly the oldest, easily the most profitable, surely the most vicious. It is the only one international in scope. It is the only one in which profits are reckoned in dollars and the losses in lives."</P>
<P>The rich gain, but the poor die in the process. In May 1932, 20,000 unemployed World War One veterans traveled to Washington, D.C., to demand the immediate payment of bonuses that the government promised them years in the future.</P>
<P>On the president's order, the veterans' encampment was attacked with tear gas, machine guns and tanks. Butler supported the protesters.</P>
<P>Butler rightly asks, "how many of these war millionaires shouldered a rifle? How many of them dug a trench? How many of them knew what it meant to go hungry in a rat-infested dugout?  How many of them spent sleepless, frightened nights, ducking shells and shrapnel and machine gun bullets? How many of them parried the bayonet thrust of an enemy? How many of them were wounded in battle?"</P>
<P>He argued that the U.S. government had to disguise the war in patriotic propaganda. "Beautiful ideals were painted for our boys who were sent out to die. This was the 'war end to wars.' This was the 'war to make the world safe for democracy.' No one told them that dollars and cents were the real reason. No one mentioned to them, as they marched away, that their going and their dying would mean huge war profits."</P>
<P>Butler is scathing in his description of how the U.S. government wasted the lives of those soldiers who died but also of those who survived. "Boys with a normal viewpoint were taken out of the fields and offices and factories and classrooms and put into the ranks. There they were remolded; they were made over; they were made to about face; to regard murder as the order of the day. They were put shoulder to shoulder and, through mass psychology, they were entirely changed. We used them for a couple of years and trained them to think nothing at all of killing or being killed. Then, suddenly, we discharged them and told them to make another 'about face!'...We didn't need them any more...Many, too many, of these fine young boys are eventually destroyed, mentally, because they could not make that final 'about face' alone."</P>
<P>However stark his criticisms of war profiteering and abuse of veterans by American militarism, Butler is no anti-imperialist. His alternatives to U.S. conquest amount to isolationism that would restrict the military to defense of the homeland and its strategic territories, including its neocolonial territories like the Panama Canal.</P>
<P>Nevertheless, his book is a stunning condemnation of U.S. militarism and it ends with a demand that rings true down to today--"To hell with war."</P>


