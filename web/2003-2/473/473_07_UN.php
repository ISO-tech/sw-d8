<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why the UN is not a solution in Iraq</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 24, 2003 | Page 7</P>

<P><font face="Times New Roman, Times, serif" size="3">LAST WEEK, the United Nations (UN) Security Council authorized a multinational occupying force in Iraq under U.S. command and set a December 15 deadline for the U.S. Provisional Authority's handpicked Iraqi Governing Council to set a timetable for a new constitution and a new government. The Security Council, in other words, gave the Bush administration everything it asked for.</P>
<P>This is a far cry from the scenario many antiwar activists envisioned if the UN intervened in postwar Iraq. "The new resolution...does nothing to make the occupation acceptable, and we remain adamantly opposed to it," wrote Middle East expert Phyllis Bennis.</P>
<P>The Bush administration sidelined the UN as it prepared to invade Iraq last spring and has denied it any meaningful role in the occupation, leading many activists to conclude that the UN could provide a positive alternative to the U.S.'s destructive role in postwar Iraq. But this latest UN Security Council resolution, following on the heels of a resolution in May authorizing the U.S. occupation, should give pause to activists who hope for a more effective UN role in curtailing U.S. imperialism.</P>
<P>The problems with the UN run much deeper than a handful of UN resolutions gone awry. The principles that the UN professes to represent--multinational cooperation and peacekeeping--are designed not to challenge but to uphold the dominance of the world's superpowers in the global status quo.</P>
<P>Its humanitarian programs such as UNICEF and treaties aimed at bettering conditions for the world's poor cannot succeed without the blessing of the superpowers. The doomed Kyoto Protocol--opposed by the U.S.--shows this clearly.</P>
<P>In recent months, the UN Security Council has been scrambling to reassert its own relevance after it refused to condone the U.S.'s timetable for invading Iraq in March. Bush's cheerleading squad at the <I>Wall Street Journal </I>commented of the latest UN resolution, "[E]ven the French decided it was more important to keep America committed to the UN process than to give the Bush administration another poke in the eye."</P>
<P>UN leaders no doubt wish to avoid the fate of the UN's predecessor, the League of Nations, which met its demise with the reshuffling of imperialist power at the end of the Second World War.</P>
<P>The UN, formed in 1945, opened membership in its General Assembly to the world's nations, but placed all decision-making power into the hands of the five major superpowers--the U.S., the Soviet Union, Britain, France and China. These five are permanent members of the UN's leading body, the Security Council, who hold veto power even over the council's 10 elected members, effectively preventing the UN from taking action against the interests of any superpower.</P>
<P>Thus Israel, a staunch U.S. ally, has flouted dozens of UN resolutions over the last of 30 years without consequence. In contrast, the UN Security Council provided the mandate for the U.S.-led 1991 Gulf War--a war that killed some 200,000 Iraqis and destroyed the entirety of Iraq's infrastructure--and 13 years of U.S.-sponsored sanctions responsible for the deaths of over a million Iraqis.</P>
<P>The sanctions were only lifted in May, at the Bush administration's request. The UN may not have endorsed the most recent U.S. war on Iraq, but it did nothing to prevent it--and now has endorsed the U.S. occupation resulting from it.</P>
<P>Far from peaceful cooperation, the UN Security Council is a snake pit of maneuvering and blackmail. When Yemen's UN ambassador voted against the resolution endorsing the Gulf War, a U.S. official told him: "That was the most expensive 'no' vote you ever cast." And the U.S. cut off millions in aid to Yemen, one of the poorest countries in the world.</P>
<P>The UN came into being to regulate the world in the interests of the victors of the Second World War, which cost the lives of millions. Since that time, there have been more than 150 wars that have killed more than 25 million people. The UN does not strive for a world of peace and cooperation, but maintains a world of imperialist war.</P>


