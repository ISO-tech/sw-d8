<P><font face="Times New Roman, Times, serif" size="5"><b>News of our struggle</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 24, 2003 | Page 11

<P><p><font face="Times New Roman, Times, serif" size="4"><b>Abolish the death penalty</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Marlene Martin</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">OPPONENTS OF the death penalty held forums and protests across the country last week as part of Death Penalty Awareness Week, called by the Campaign to End the Death Penalty. Many chapters of the Campaign held successful teach-ins and "Live From Death Row" events, where a death row prisoner spoke to the audience via speakerphone.</P>
<P>The week culminated with rallies and protests in several cities.</P>
<B><P>Austin, Texas</B><BR>
Some 300 chanting protesters led a march for a moratorium past the governor's mansion and Capitol to rally in front of the federal court building. Speakers included Clarence Brandley, an exonerated Texas death row inmate; Sandra Reed, the mother of death row prisoner Rodney Reed; as well as other activists, lawyers and abolitionist groups. </P>
<P>The march was very upbeat, according to Lily Hughes, a Campaign organizer. "The speakers emphasized how much progress we have made on this issue, even here in Texas--the execution state," Hughes said. "The feeling among the crowd was very optimistic."</P>
<B><P>Chicago</B><BR>
As many as 150 protesters gathered at Chicago police headquarters on the city's South Side to march against police torture and call for an end to the death penalty. "We know that these cops have brutalized and tortured confessions from people, and some of those people ended up on death row," Joan Parkin of the Campaign told the crowd. "But where are the cops? The ringleader Jon Burge is enjoying a full pension, while others are still on the force." </P>
<P>"I can't talk about Ronnie's torture because I will just start crying," said Louva Bell, mother of Death Row 10 member Ronald Kitchen and one of many family members who shared their powerful testimonies. "It's going to take all of us--we all have to come together and keep fighting," she told the crowd.</P>
<B><P>San Quentin, Calif.</B><BR>
About 40 people held a spirited demonstration outside San Quentin Prison, which houses the largest death row in the country. Family members, activists and lawyers spoke at the demonstration, underlining the absurdity of a state with a terrible budget crisis spending millions of dollars to expand death row.</P>
<B><P>Baltimore</B><BR>
Some 40 people turned out for a rally at the Supermax prison in Baltimore, the site of Maryland's death row, which is a testament to the racism of the death penalty. Of the 10 people on Maryland's death row, six are Black--and all were condemned for killing a white person.</P>



