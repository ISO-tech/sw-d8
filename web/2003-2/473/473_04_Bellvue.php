<P><font face="Times New Roman, Times, serif" size="4">Construction site closed after <I>SW</I> story on risk of collapse</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Will Bellevue be fixed?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 24, 2003 | Page 4

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,<br>
</B></I>Thanks to <I>Socialist Worker</I> newspaper, a sign on the locked door of room CD421 of Bellevue Hospital reads: "Vacate: Do  Not Enter. The Department Of Buildings Has Determined That Conditions In This Premises Are Imminently Perilous To Human Life," and is signed "By Order of the Building Commissioner."</P>
<P>Two days after <I>SW</I> ran a story about the risks of the CD building at Bellevue Hospital falling down, a flurry of activity began. Two building inspectors hired by New York University (NYU), which leases space in the CD building, showed up at 6 p.m. on September 26, and ordered the construction site that had endangered the building shut down "forthwith."</P>
<P>It's stayed closed down. On September 30, Bellevue Hospital management posted notices that inspections had found no risk of immediate collapse, but that "the building is being monitored continuously and is still safe to occupy."</P>
<P>Just when everybody was feeling a bit better, on October 6, the notice about conditions "perilous to life" went up on the door of CD421. Nobody, so far, is offering to explain the contradiction.</P>
<P>One manager, however, did say that the construction site was closed down after "some socialists sold some kind of newspaper about all this at the NYU School of Medicine." (Hardly surprising, since some people from the NYU Medical School work in the Bellevue CD building.)</P>
<P>What's next? Nobody knows for sure. Workers are busy this week putting plywood over the huge crack on the outside of the building, so it can't be seen. The top is still uncovered, and the crack is obviously bigger.</P>
<P>One safety inspector told me, "Wait till a nice big wet snow hits. This building can't stand the load from a foot of wet snow on the balconies, the window sills and the roof." Time will tell.</P>

<B><P>Thomas F. Barton</B>, AFSCME DC 37, Local 768, New York City</P>


