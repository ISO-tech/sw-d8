<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>What workers gain from strikes</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 24, 2003 | Page 9</P>

<P><font face="Times New Roman, Times, serif" size="3">DURING THE past few decades of working-class retreat, it has become fashionable to argue that strikes are--to cite the learned opinion of one labor relations professor--an "an outdated labor model."</P>
<P>It's certainly true that the volume of strikes and the number of workers involved in strikes has declined in this same period. But it's no coincidence that this period of decline in union membership and union clout has been accompanied by fewer and fewer strikes.</P>
<P>The two things are entirely linked. Workers who are organized are able to extract better conditions from employers than workers who are not--but only if are organized to engage in <I>effective</I> <I>action</I> rather than negotiate the terms of retreat.</P>
<P>Employers constantly try to lower their labor costs by cutting wages and increasing the pace of work, because in this way they can increase profits. Workers, if they are not to sink to bottom, must combine and resist in order to push in the other direction. This is, at the most basic level, what constitutes the class struggle.</P>
<P>Strikes are, according to Frederick Engels, "the first attempt of the workers to abolish competition" between themselves and replace it with solidarity. But solidarity for what purpose?</P>
<P>When labor organizes and refuses to work, the wheels of industry cease to turn, and the bosses' source of profits dries up. Thus, the strike sparks fear into capitalists because it's a reminder, to quote Lenin, that "it is the workers and not they who are the real masters."</P>
<P>Workers first engage in strike because they are the only means to secure immediate redress against their employer. But as an act of collective solidarity, strikes help workers identify their common interests with the whole working class.</P>
<P>"A strike, moreover," wrote Lenin, "opens the eyes of the workers to the nature, not only of the capitalists, but of the government and the laws as well." Workers who strike quickly learn that the police and courts are set up against them and for the bosses. The fact that workers must themselves suffer hardship in order to carry out a strike only increases their sense of solidarity and self-sacrifice.</P>
<P>But strikes are not only important because they hold the greed of capitalists in check and can lift wages and conditions. They are, to quote Engels again, "military schools of the workingmen in which they prepare themselves for the great struggle which cannot be avoided."</P>
<P>Strikes, in other words, are the only means by which workers can measure their own strength and the strength of their opponent, and learn from their own experience how to carry on the fight more and more successfully.</P>
<P>Marx criticized "the class of philanthropists...who consider strikes as very mischievous to the interests" of workers. The significance of strikes should not, Marx argued, be measured "by the apparent insignificance of their economic results." He argued that the "conflict between masters and men" was "the indispensable means for holding up the spirit of the laboring classes."</P>
<P>The Russian revolutionary Lenin put it another way. As individuals, workers remain "veritable slaves. But when the workers state their demands jointly and refuse to submit...they become human beings, they begin to demand that their labor should not only serve to enrich a handful of idlers, but should also enable those who work to live like human beings."</P>
<P>In other words, strikes have a tremendous moral influence on workers, giving them a sense of confidence in their own capacities to organize and build a different world for themselves. "Behind every strike," a Prussian minister once warned, "lurks the hydra of social revolution."</P>
<P>Unions, as Marx noted, are important "as a means of organizing the working class for struggle against the bourgeoisie." This is precisely what unions in the U.S. must become if they are to reverse labor's long decline, and the most effective and important means of struggle is the strike.</P>


