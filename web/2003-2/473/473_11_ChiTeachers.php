<P><font face="Times New Roman, Times, serif" size="5"><b>Chicago teachers say "No deal!"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jesse Sharkey, CTU Delegate</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 24, 2003 | Page 11</P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--Members of the Chicago Teachers Union (CTU) voted 15,965 to 10,723 to reject the leadership's contract proposal October 16, sending shock waves through the union and the city. The CTU membership has not rejected a proposal that was backed by the union leadership in anyone's memory.</P>
<P>The vote was all the more remarkable because the union leadership, under CTU President Deborah Lynch, had pushed so hard to pass it. In the two weeks leading up to the vote, the union's officers and staff visited 130 schools, made thousands of phone calls, produced a pro-contract video, launched a Web site and mailed glossy brochures to all 33,000 active members.</P>
<P>But the rank and file poured out years of anger and frustration in the vote, sweeping schools that had opposed and supported Lynch alike. Meanwhile the city newspapers are raising the prospect of the first teachers' strike in 16 years. A strike may be necessary to get the contract that we need and deserve.</P>
<P>But first our union needs a real contract campaign. Our membership is divided, and many are alienated by leadership's hard sell of the deal, which contained concessions on health care and didn't address key issues like class size. During the contract vote, Lynch said there was nothing to get from further talks with the school board--so that a "no" vote equaled a vote to strike.</P>
<P>Lynch was elected in 2001 on a reform slate that promised to stand up to the school board after years of terrible contracts and the misuse of union funds by the CTU's old guard. The former leadership and its supporters have opposed Lynch at every step--and organized extensively against her proposed contract. </P>
<P>But the "no" vote went far beyond the old guard. In hundreds of schools, people were angry about the proposed contract and wanted to push for something better.</P>
<P>Now Lynch has announced a return to negotiations. But the only way forward for our union is a real contract campaign. If Lynch doesn't agree to such a mobilization at the special House of Delegates meeting October 29, rank-and-file activists must be prepared to organize one independently.</P>
<P> A contract campaign would work towards a strike authorization vote, but initially focus on activating, educating and mobilizing the membership. Every school should have union buttons and contract campaign literature, including pamphlets explaining why our contract demands are just--and figures showing why the city can afford it.</P>
<P>The union should plan a series of public events designed to reach out to the public--visit Local School Council meetings, leaflet churches and neighborhood meetings and reach out for support from other organizations. The union should organize informational picketing at the Board of Education, hold a press conference at an over-crowded school and call a mass rally at Mayor Richard Daley's office.</P>
<P>Rank-and-file members must be involved at every step. The "no" vote showed the potential for an inspiring fightback, but we need a real mobilization to go forward.</P>



