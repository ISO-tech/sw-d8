<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Bush leads the way in spewing bigotry against gays and lesbians</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Hypocrites on the loose</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 8, 2003 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">THE RIGHT-wing hypocrites in Washington are on the loose--and George W. Bush is leading the way.</P>
<P>When White House spin doctors wheeled Bush out for a press conference last week--only the eighth of his presidency--they must have worried that reporters would demand answers about the scandalous lies that their boss told to justify war on Iraq. But the media "watchdogs" let Bush off the hook about Saddam's nonexistent weapons of mass destruction.</P>
<P>Instead, the headlines about the press conference the next day focused on Bush's smirking denunciation of gay marriage. "I believe a marriage is between a man and a woman," Bush said. "And I think we ought to codify that one way or the other. And we've got lawyers looking at the best way to do that."</P>
<P>Bush knew exactly what he was doing. Ever since the U.S. Supreme Court's decision in July to overturn anti-sodomy laws, right wingers have been frothing at the mouth over the idea that the decision might pave the way toward increased legal rights for same-sex couples, like the right to marry or adopt children.</P>
<P>How dare these people--the same ones who led the charge for a brutal war on defenseless Iraqis and who shortchange our schools to hand out tax breaks for the rich--lecture anyone about "morality" or "decency"?</P>
<P>Not to be outdone, the Vatican got into the gay-bashing act, too. "Marriage is holy, while homosexual acts go against the natural moral law," read the Vatican statement--which added that "allowing children to be adopted by persons living in such unions would actually mean doing violence to these children."</P>
<P>How nauseating! This is the very church hierarchy that has been exposed for systematically protecting pedophile priests as they abused children for years. And they have the gall to demand a ban on gay marriage to "protect" children.</P>
<P>Bush's press conference statement was a signal to his supporters to go on the offensive with more anti-gay attacks. Senate Majority Leader Bill Frist (R-Tenn.), for example, says that Congress may take up a proposed Constitutional amendment that would define marriage as only between a man and a woman. </P>
<P>But if the Republicans are on the rampage, Democratic Party leaders were quick to fall in line behind the talk about the "sanctity of marriage."</P>
<P>They have a good precedent to look to, after all. Bill Clinton stabbed gay and lesbian supporters in the back when he implemented the disastrous "don't ask, don't tell" policy against gays in the military. And he signed the shameful Defense of Marriage Act, which denies federal recognition of same-sex marriages and allows states to ignore same-sex unions licensed elsewhere.</P>
<P>Meanwhile, most of the announced candidates for the Democratic presidential nomination refuse to support the right to same-sex marriage. Supposed liberals like former Vermont Gov. Howard Dean and Rep. Dick Gephardt (D-Mo.)--whose own daughter is a lesbian--say that they favor "civil unions" for gays and lesbians.</P>
<P>This is a cop out. Civil unions would give gays and lesbians some legal rights, while reserving marriage as a special status for heterosexuals alone. No one should be able to tell anyone else who they can or can't marry--certainly not the hypocrites and liars in Washington.</P>
<P>We can't allow this backlash against gays and lesbians to go unchallenged. The way to push back the right wing--and ultimately to win real equality for gays and lesbians--is to rebuild a fighting movement that looks to its own organizing, rather than putting its hopes in politicians.</P>

