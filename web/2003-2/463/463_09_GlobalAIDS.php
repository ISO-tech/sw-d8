<P><font face="Times New Roman, Times, serif" size="5"><b>A resource in the struggle against AIDS</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Naveen Jaganathan</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 8, 2003 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">Alexander Irwin, Joyce Millen and Dorothy Fallows, <B><I>Global AIDS: Myths and Facts.</B></I> South End Press, 2003, 267 pages, $19.</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>THE THREAT of Iraqi weapons of mass destruction wasn't the only lie George W. Bush told in his State of the Union address. He also promised to set aside $15 billion to fight the AIDS epidemic in Africa and the Caribbean. In reality, just $200 million will be allocated to the Global Fund to fight AIDS, Malaria and Tuberculosis--the organization that combats AIDS in developing nations. </P>
<P>In the context of the lies that politicians weave about AIDS, <I>Global AIDS: Myths and Facts </I>sets out to destroy a number of these myths. Moreover, the book attempts not only to raise awareness but activism. As Zackie Achmat argues in the preface, the movement against AIDS must adopt "the words of the labor movement, 'an injury to one is an injury to all.'"</P>
<P>The book takes up 10 myths that shape the common misconceptions about AIDS, from the that AIDS is an "African problem" to the belief that corporations and governments are too powerful to bend to the needs of the poor.</P>
<I><P>Global AIDS</I> confronts the prevalent idea that AIDS is a result of dangerous personal behaviors--promiscuous sex, drug use that people should "just give up"--head on. Instead of shifting the blame for the spread of AIDS onto individuals, the authors turn to a number of economic and social factors that define personal behavior. For example, poverty--not personal choice--pushes women into high-risk jobs as sex workers.</P>
<P>The responsibility must also fall on heavy debt burdens imposed by the international financial institutions like the International Monetary Fund, which force poor nations to cut state education and health care programs, leaving already vulnerable populations at further risk.</P>
<P>By providing examples of past successes--including the movement against intellectual property rights, which prohibit poor nations from developing generic versions of anti-retroviral drugs--this book charts a way forward toward building a mass movement to confront AIDS.</P>

