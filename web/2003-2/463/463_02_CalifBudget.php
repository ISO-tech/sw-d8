<P><font face="Times New Roman, Times, serif" size="5"><b>Paying the price for Davis' budget deal</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Rachel Odes</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 8, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">MYRON GEE is taking a second job to try to pay for his son's education at the University of California-Berkeley. That's the direct result of the budget deal that Gov. Gray Davis made with Republican and Democratic lawmakers at the end of July.</P>
<P>The "compromise" will slash billions of dollars in spending on social services and education. In particular, the state's nine-campus University of California (UC) system and the 23-campus California State University (CSU) system will lose more than $700 million in funding--after years of being shortchanged by the politicians in Sacramento to begin with.</P>
<P>The two systems will make up the difference by sharply increasing tuition costs and fees. Tuition at UC campuses will rise by more than $1,000 this year alone, making the average in-state tuition cost for undergraduates $5,500. CSU campuses now charge an average of $2,500 for undergraduates.</P>
<P>All this "defeats the purpose of why the public universities were set up," Gee told the <I>San Francisco Chronicle.</P>
</I><P>CSU administrators plan to cut enrollment growth in half for the next year, and the UC system is targeting educational outreach for cuts of 50 percent and more. This means that minority and poor students will be even more excluded from California state campuses--where officials axed affirmative action programs in the mid-1990s. </P>
<P>Meanwhile, the UC regents are even delaying the opening of a new campus at Merced by another year, stranding the 1,000 students who planned to enroll this fall.</P>
<P>Many students hope to make up for the increases by taking out more loans and working longer hours. But the state shouldn't make students and the poor pay for the budget shortfall. </P>
<P>California's filthy rich have plenty of money--if Davis and Sacramento politicians would reverse the tax giveaways that they've handed out over the past decade and more. We have to prepare for a fight--and tell the politicians that we won't pay for their crisis.</P>

