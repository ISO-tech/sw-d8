<font face="Times New Roman, Times, serif" size="5"><b>The sick men who run this country</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 8, 2003 | Page 1</font></p>

<P><font face="Times New Roman, Times, serif" size="3">IS THE Bush gang as cynical and bloodthirsty as we thought? You bet your life. Which, by the way, is what the Pentagon had planned.</P>
<P>Until their scheme was exposed last week, the Defense Advanced Research Projects Agency (DARPA), the Pentagon's research arm, was about to open up its own online futures betting market. But instead of taking bets on grain or hog prices, the Pentagon brass were preparing to gamble on future terrorist attacks.</P>
<P>"[T]raders bullish on a biological attack on Israel, say, or bearish on the chances of a North Korean missile strike, would have had the opportunity to bet on the likelihood of such events on a new Internet site established by the DARPA," explained the <I>New York Times.</I> You could also wager on whether Palestinian leader Yasser Arafat would be assassinated--or if the king of Jordan would be deposed.</P>
<P>Before the project was exposed, the Pentagon had already spent more than $500,000 to develop the system--and had requested $8 million more over the next two years.</P>
<P>All this was the brainchild of Adm. John Poindexter, Ronald Reagan's national security adviser best known for lying to Congress about the Iran-Contra scandal of the 1980s. Poindexter's sick plan was too much for even the Bush White House to stand by. The old Contragate crook was forced to resign.</P>
<P>But this is hardly the scariest scheme that Poindexter and his pals have come up with. As director of the Terrorism Information Awareness Office, Poindexter proposed to develop a Big Brother program to spy on people by tapping into online databases. Meanwhile, his buddies at the Pentagon are pushing ahead with an insane plan to manufacture "mini-nukes"--smaller-yield nuclear weapons that the maniacs in Washington hope to actually use in future wars.</P>
<P>This administration lied its way into an invasion of Iraq--and gambled with thousands of Iraqi lives in a war for oil and empire. At home, it's playing a shell game with government spending--stealing money meant for schools and social services to dole out to the rich in tax cuts.</P>
<P>We've had enough of these monsters who gamble with the lives of ordinary people around the world.</P>

