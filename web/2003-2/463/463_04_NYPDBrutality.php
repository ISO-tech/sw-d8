<P><font face="Times New Roman, Times, serif" size="4">Calvin Washington died because of the NYPD</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Police on the rampage</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 8, 2003 | Page 4</font></p>

<font face="Times New Roman, Times, serif" size="3"><P><B>Dear <I>Socialist Worker,</I></B></P>
<P>For the third time in two months, an innocent New Yorker has fallen victim to hasty, goon-like actions of the New York City Police Department. Calvin Washington died on June 28 from a heart attack, 10 days after police raided his apartment in Brooklyn. </P>
<P>Police suspected that 21-year-old Taheem, the son of Calvin and his wife Yvonne, was armed and had run inside the brownstone where the family lived, escaping through a back window. Police knocked down the door and physically attacked the family. "When I got to the door, there were already 15 to 20 cops standing inside the house," said Yvonne Washington in the <I>New York Amsterdam News</I>. "They didn't say anything about why they were there at that moment."</P>
<P>Following the initial rush, more than 40 cops invaded the home. They cursed at Yvonne, throwing her down on the floor. They threatened 22-year-old son Matthew and assaulted 7-year-old daughter Nashay. On their way upstairs, one officer stomped one of the family's cats to death. </P>
<P>In shock, Calvin Washington--who had a serious heart condition and had recently had pacemaker surgery--suffered a heart attack. Days later, he died. </P>
<P>New York City Mayor Michael Bloomberg publicly defended his "urban army" and said that the raid was justified. But this is not the first police manhunt to go awry. In May, Alberta Spruill died of cardiac arrest when the NYPD smashed down her door and threw a stun grenade into her apartment after they received incorrect information. That same week, cops shot African immigrant Ousmane Zongo to death while raiding a warehouse in Manhattan. </P>
<P>Calvin Washington is New York City's fourth known death resulting from botched police procedures this year. Yet to date, all of these killings have been ruled accidents.  </P>
<B><P>Dan Clemente, </B>New York City</P>

