<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>How the capitalist system was born</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 8, 2003 | Page 9</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>"THE ECONOMIC structure of capitalist society has grown out of the economic structure of feudal society," Karl Marx wrote. "The dissolution of the latter sets free the elements of the former."</P>
<P>The essential condition for capitalist production was the separation of the direct producers from ownership of the means of production on one side, and the concentration of all the raw materials and instruments of production in the hands of a few rich capitalists on the other. It was this separation of the majority from ownership of land or tools that created the basis of our modern system of wage labor--where the vast majority of people are forced to sell their labor power to survive.</P>
<P>In the early stages of feudalism, the vast bulk of production was directly for use, rather than exchange--whether it was grain produced for the peasants' subsistence, or the extra surplus that went to the lord or king. Peasants were tied to the soil, and artisans owned their own tools and workshops.</P>
<P>But as peasant agriculture became more productive, the market economy began to increase in size and importance. Households increasingly turned to the market to buy the things that they no longer produced for themselves. This meant a growth of artisans in the towns and a bigger market in turn for agricultural commodities. </P>
<P>This market economy grew up not only in Britain, but also in the Netherlands, and in Italy, Portugal and Spain. A new merchant class, increasingly rich and powerful, emerged with an interest only in ways to make more money through trading.</P>
<P>But there was a problem with merchant capitalism. Though a merchant could make a quick fortune through buying and selling, market fluctuations could destroy their profit margins.</P>
<P>One way that merchants got around this was by conquest. By seizing control of India, for example, British merchants could simply steal--or at the very least extort at a below-value price--goods produced in India, which they could then sell in England for a handsome profit. </P>
<P>But an even more surefire way to guarantee sustained profits would be to control the production of the goods themselves. At first, merchants used the "putting-out" system--in which they would loan raw materials to workers, who would then turn out the product and receive a small part of the proceeds. From here, it was a small step to bring the workers together under one roof, where they would produce products for a wage.</P>
<P>With new capitalist enterprises developing in the towns, there was a growing need for a labor force that was wasn't tied to the land. Over a period of a few centuries in England, peasants were driven off of their land, and artisan production was broken up in villages. This created a class of propertyless vagabonds whose only choice would be to seek work as wage laborers in the towns.</P>
<P>This new class of wage workers was created by outright theft. Rich landowners stole the common lands that peasants depended on for their livelihood--a process that went on over several centuries. But even this process wasn't sufficient to guarantee that people would be willing to work long and hard hours for poverty wages. Under Britain's King Henry VIII and subsequent rulers, therefore, laws were passed imposing harsh sentences--including death--on anyone caught begging. Some 72,000 people were executed as vagrants under Henry VIII, for example. </P>
<P>Marx notes the absurd irony in this: "Thus were the agricultural people, first forcibly expropriated from the soil, driven from their homes, turned into vagabonds, and then whipped, branded, tortured by laws grotesquely terrible, into the discipline necessary for the wage system."</P>
<P>This violent wrenching of millions of peasants off the land was a necessary condition for the development of wage labor--which is the essential condition of modern capitalism. </P>

