<font face="Times New Roman, Times, serif" size="5"><b>"Pressure from below exposed the lies"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 8, 2003 | Page 6</font></p>

<font face="Times New Roman, Times, serif" size="3"><P><b>MIKE MARQUSEE</b> is a leading member of the Stop the War Coalition in Britain. After speaking at an antiwar meeting in New York City last month, he talked to <I>Socialist Worker's </I>LUCY HERSCHEL<I> </I>about the scandal facing British Prime Minister Tony Blair's government--and the future of the struggle against war.</P>

<P><b>WHY HAVE mainstream media outlets like the BBC become so critical of a war that they supported when it was happening?</b></P>

<P>TRADITIONALLY, IN times of war, the BBC has been the voice of the pro-war party and the government, and has rallied support behind the troops. That was largely true during the invasion of Iraq. </P>
<P>However, the existence of a huge movement that was visible in the streets of London and every major town and city in the country put pressure from below on the BBC. First, it stiffened the BBC's backbone and many journalists who had criticisms of Blair regime, but had been reluctant to voice them. It also became apparent that if the BCC was to retain credibility as a public broadcasting network, it had to reflect the reality of mass dissent. </P>
<P>The upshot was that after the war was over--or the first phase of the war was over--they broadcasted very serious criticism of Blair and the way the country had been led into war. </P>
<P>Within parliament, there is no opposition to the war because the official opposition party, the Tories, support the war. Therefore, parliament doesn't reflect the spectrum of public opinion, in which there is massive opposition. The BBC and certain other sections of the media have taken up the duty of the opposition. </P>
<P>That hasn't happened in this country in part because you don't have a public broadcasting system. The BCC is a very flawed and deeply elitist institution, but ultimately, it is accountable to the public and not to advertisers. For all its faults, it is more accountable to the public than any media in the U.S. can be. The big lie of capitalism, in relation to the media, is that a private press is a freer press. The reality is that a privately owned media is actually less independent. </P>

<P><b>THERE IS a debate in the antiwar movement about whether to call for an immediate end to the occupation and withdrawal of U.S. troops from Iraq, or whether that would leave Iraq in even more chaos than it is now. What do you think? </b></P>

<P>YOU SHOULD be aware that in the international antiwar movement, the overwhelming consensus is for an immediate end to the occupation and the unconditional withdrawal of U.S. and British troops. </P>
<P>The arguments for the occupation are essentially anti-democratic arguments. The Iraqi people are perfectly capable of governing themselves if they are allowed to do so. It is at the moment the U.S.-British occupation that stands between the Iraqi people and self-government. The occupiers are not accountable in any way to the people whose land they occupy. And therefore, there will be resistance to occupation. </P>
<P>This is a central question of the antiwar movement because it goes to the heart of our criticism of imperialism. You have to be clear that the lessons of human history show that there is no such thing as a benign imperialism. </P>

<P><b>HOW DO people in Europe and the rest of the world view the antiwar movement in the U.S.?</b></P>

<P>THE ANTIWAR movement in the U.S. faces the greatest challenges of any section of antiwar movement. It also has the greatest responsibility. All over the world, people know that we must have a change within the political balance in the U.S. if we are to restrain the American empire. </P>
<P>It's been said by Tony Blair that the antiwar movement in Europe is anti-American. That's a calculated lie. The antiwar movement in Europe is, of course, opposed to the policies of the American elite, but it is reaching out to American working class people and the American opposition to the war. All over the world, examples of American dissent are enthusiastically welcomed. Michael Moore's book <I>Stupid White Men </I>sold 600,000 copies in Britain alone. And the news of the huge turnout in New York City on February 15 was enormously inspiring to people all over the world. </P>
<P>Far from being anti-American, people all over the world look to the forces of dissent in American as their greatest hope. </P>
<P>It's very important now that the American antiwar movement build strong and vital links with the rest of us around the world. Our movement will be more effective to the extent that we work together and coordinate our actions. </P>
<P>Bush and Blair have their special relationship. We internationalists need to build our own special relationship at the grassroots. </P>

