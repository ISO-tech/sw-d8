<P><font face="Times New Roman, Times, serif" size="5"><b>Another gift for the oil boys</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 8, 2003 | Page 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3">GEORGE W. BUSH is getting ready to reward his rich oil buddies yet again--this time by approving a $1.6 billion natural gas project in Peru.</P>
<P>If approved, the Camisea project--with reserves of 13,000 billion cubic feet of gas--will benefit two Texas energy companies with longstanding ties to the Bush administration: Hunt Oil Co. and KBR (formerly Kellogg Brown &amp; Root), a division of Halliburton. </P>
<P>Ray Hunt, chairman of Hunt Oil, raised more than $100,000 for Bush's election campaign in 2000. And Vice President Dick Cheney used to be the CEO at Halliburton.</P>
<P>If the Camisea project goes ahead, KBR would construct a $1 billion natural gas plant on the Peruvian coast. The consequences for the environment and native Peruvians if the pipeline deal is completed will likely be devastating. </P>
<P>Environmentalists point out that the pipeline cuts through the Paracas National Reserve, Peru's only marine sanctuary, which is home to rare and endangered species. "Access will lead to continued loss of forest cover, wildlife habitat loss, fragmentation, reduced forest biodiversity and reductions in populations of important plant and animal species over large areas," says an internal report from the U.S. Export-Import Bank. "Such effects are negative, significant, long term and largely irreversible without effective mitigation through project operation and after closure." Meanwhile, thousands of indigenous Peruvians would likely be displaced.</P>
<P>This week, the boards of the U.S. Export-Import Bank and the Inter-American Development Bank (IDB) will vote on whether or not to approve more than $300 million in loans for the project.</P>
<P>Don't be surprised if they rubber-stamp the deal. It just so happens that Export-Import Bank President Phillip Merrill is a friend of Dick Cheney's--and the chief U.S. representative at the IDB, Jose Fourquet, campaigned for Bush in 2000.</P>

