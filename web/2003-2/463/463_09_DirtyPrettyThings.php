<P><font face="Times New Roman, Times, serif" size="4">New movie thriller <I>Dirty Pretty Things</I></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"The people you don't see"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Jennifer Roesch</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 8, 2003 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2"><B><I>Dirty Pretty Things,</B></I> directed by Stephen Frears, screenplay by Steven Knight, starring Audrey Tautou, Chiwetel Ejiofor, Sergi L&oacute;pez and Sophie Okonedo.</font></P>

<font face="Times New Roman, Times, serif" size="3"><P><I>DIRTY PRETTY Things</I> is one of those rare movies that treats those who are oppressed and exploited by the system not simply like victims but as heroic subjects.</P>
<P>Director Stephen Frears has created a richly textured portrait of illegal immigrants struggling to survive on the margins of London society. The characters in this film struggle to protect fragile bonds of solidarity and maintain their humanity in the face of brutal and dehumanizing circumstances.</P>
<P>Okwe is the emotional and moral center of the filmHe is a Nigerian immigrant who used to be a doctor but now works two jobs--one as a livery cab driver and the other as a night clerk in a hotel. He sleeps a few hours a night on a rented couch. In his first line of the film, he tells two passengers, "I am here to rescue those who have been let down by the system."</P>
<P>One night, Okwe is called to fix a clogged up toilet in one of the hotel roomsThere, he finds there a human heart.</P>
<P>It turns out that the manager of the hotel, Sneaky, is running another business in which he coaxes desperate immigrants to have their organs removed in exchange for a passport. When Sneaky learns that Okwe used to be a doctor, he tries to force him to participate in the scheme.</P>
<P>The movie follows Okwe as he attempts to negotiate a series of non-choices that threaten to destroy him and those around him.</P>
<I><P>Dirty Pretty Things</I> shows the potential that people have to survive and resist in the most wretched of circumstances. The characters in the film form fragile bonds with one another as they attempt to protect each other as best they can.</P>
<P>Senay is a Turkish refugee who is hounded by the immigration authorities and loses two jobs and her home as a result. Guo Yi is an orderly at the hospital morgue who plays chess with Okwe and steals him medicine to treat other immigrants. And Juliette is a prostitute who works at the hotel. In the course of the film, the three band together to try to take some kind of control over their lives.</P>
<I><P>Dirty Pretty Things</I> is a gripping thriller but is also a scathing indictment of the treatment of immigrants in our society. At one point Okwe says, "We are the people you don't see." This film allows the people we don't see to tell their story. In a time in which immigrants are brutalized and scapegoated by the media and politicians, a film that sensitively portrays their struggles could not be more welcome.</P>

