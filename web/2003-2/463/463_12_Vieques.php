<P><font face="Times New Roman, Times, serif" size="4">Activists face up to 20 years in jail</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Defend the Vieques 12!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Roberto Barreto</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 8, 2003 | Page 12</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>THE FEDERAL government is trying to take its revenge on the movement that drove the U.S. Navy off its base on the Puerto Rican island of Vieques. Twelve anti-Navy activists are facing serious felony charges--including conspiracy and destruction of federal property--that carry sentences of five to 20 years in federal prison.</P>
<P>The charges stem from a demonstration on May 1 called to celebrate the Navy's pullout from Vieques, which had been used for years for target practice by the U.S. military. Thousands of people came to the site to take down the military's fence and take control of the land. </P>
<P>But when they arrived, demonstrators found that another federal agency, the U.S. Fish and Wildlife Service, was keeping control of the base, claiming that it would now be turned into a nature preserve. Infuriated, the crowd tore down the entrance gates, demolished the guard's station and set fire to several military vehicles.</P>
<P>They were destroying symbols of their oppression--an installation that poisoned their environment, strangled their economy and destroyed their health, causing the highest cancer rate in Puerto Rico.</P>
<P>By all accounts, the rioting was spontaneous. But the federal government and its lackeys in the colonial government of Gov. Sila Calder&oacute;n want to make us believe that it was planned and organized by a small group of conspirators. The feds want to criminalize the Vieques struggle so that they don't have to pay to clean up the land--and can prevent the example of Vieques from inspiring new struggles.</P>
<P>In the past, each time a social or political movement has threatened U.S. control in Puerto Rico, Washington's response has been to jail its leaders. In the early 1950s, Puerto Rican nationalists received the longest jail sentences of any political prisoner in the Western Hemisphere. Likewise, in the 1980s, Puerto Rican political prisoners received disproportionately long sentences for their participation in the struggle for independence. Many of them remain incarcerated today. In 2001, Pedro Col&oacute;n Almenas, a young university student, was jailed for opposing the use of the state university for military education.</P>
<P>Now 12 activists--including well-known figures such as Nilda Medina, a Vieques organizer, and teachers' union leader Jes&uacute;s Delgado--face serious charges for a justifiable action that thousands of people participated in.</P>
<P>The federal government has the gall to accuse these activists of property destruction. But as former U.S. Attorney General Ramsey Clark put it, "It has been the U.S. Navy that has contaminated Vieques with toxic materials and live munitions, not the Committee for the Rescue and Development of Vieques, nor any other Vieques resident."</P>
<P>It's urgent to organize an international defense campaign to support the Vieques 12. The victory in Vieques over the Navy at the same time that the U.S. government was conducting its brutal war on Iraq is an example of how imperialism can be defeated. By going after the Vieques 12, they want to bury this victory. We can't let them!</P>

