<P><font face="Times New Roman, Times, serif" size="4"><b>Socialists need to support Kucinich</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P><B>Dear <I>Socialist Worker,</I></B></P>
<P>As a progressive voter with a decidedly "socialist" perspective, I would like to comment on your article about not voting for the Democrats (<I>SW</I>, July 18). </P>
<P>Dennis Kucinich would be the answer to all our problems, yet your take on him only mentions his one "error"--that he did not, earlier in his career, believe in the right to choose abortion. Everyone's opinions evolve. The rest of his platform is just what a socialist would believe. </P>
<P>I think it is a mistake to downplay him and especially to badmouth him. He might be the only hope this country has! </P>
<P>I feel fear and trepidation to think that Bush would be elected to a second term. Socialists need to reconsider what is important right now. We can have the "power of the people" come through in this next election--but we won't if there is bickering over small points without considering the bigger picture. </P>
<P>We must get Bush out--but we can't have another "centrist" in office either! Kucinich is <I>not</I> a centrist. He is for labor. He is for the environment. He is for cutting military spending and for cutting back the Pentagon's budget to filter those monies into education. He is for women's rights. He is for funding Head Start and other social programs for the poor. In fact, he is for national health care--which will help everyone, especially the poor. And he is for sustainable energy and breaking the backs of the big corporations. </P>
<P>What else do we want?</P>
<P><B>Paula Morris, </B>Park Forest, Ill. </P>

