<p><a name="Paying"></a><font face="Times New Roman, Times, serif" size="4"><b>Paying for Bush's campaigning</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P><B>Dear <I>Socialist Worker,</I></B></P>
<P>George W. Bush billed the U.S. when he was out campaigning during the 2002 elections. A report issued by the General Accounting Office (GAO) says that the Department of Health and Human Services picked up the tab at least 15 times when Bush went out of town. </P>
<P>Despite claims that this had nothing to do with Bush's campaigning appearances, the GAO still can't explain why Bush spent over $523,000 on these trips or why 14 of these 15 trips were during the election season last November. These trips coincided with campaign appearances for at least nine Republican gubernatorial candidates--including Scott McCallum in Wisconsin, Bob Taft in Ohio, Mark Sanford in South Carolina, and, of course, brother Jeb in Florida.</P>
<P>Unfortunately, it's nothing new for presidents to abuse and misuse funds from an agency that should be spending money on sick and poor people. The GAO also discovered that Bill Clinton billed the Department of Health and Human Services on at least 37 occasions during his second term in office for various political events.</P>
<P>Once again, the White House has shown its complete hypocritical ability to make campaign promises--misusing money that could be spent on those promises. Isn't it great to be in a country where we pay to hear Bush lie to us?</P>
<P><B>Ben Larrivee, </B>Boston</P>

