<p><a name="Fight"></a><font face="Times New Roman, Times, serif" size="4"><b><I>Socialist Worker </I>shows how to fight</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P><B>Dear <I>Socialist Worker,</I></B></P>
<P>I wanted to share the recent experience of what it has been like selling <I>SW </I>in San Francisco. Several Saturdays ago, nine of us gathered for a noon sale in the Excelsior district of San Francisco--a multiracial, working-class neighborhood. </P>
<P>The front cover of the issue that called Bush a "Liar!" resonated very well with many of the people who passed by us. People we met on the sale expressed an extraordinary amount of anger at Bush, but were unsure about a real solution to the mess. By drawing out people more, we were able to talk about our alternative to the barbarism of capitalism. In the end, we were able to sell 28 copies of <I>SW</I>, and seven copies of our Spanish-language paper <I>Obrero Socialista</I>. </P>
<P>Many people are angry about the tax cuts and the occupation of Iraq, and are beginning to draw the right conclusions. But they are still not sure what they can do about it. </P>
<P>That is the crucial role that <I>SW</I> takes on. It shows people that we can actually make a difference, and that a different world is possible--and necessary.</P>
<P><B>Ed Hernandez, </B>San Francisco</P>

