<p><a name="Organize"></a><font face="Times New Roman, Times, serif" size="4"><b>Organize for strikes, not for Democrats</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P><B>Dear <I>Socialist Worker,</I></B></P>
<P>I am writing this to say that rank-and-file union members must put pressure on union officials to show that supporting the Democratic Party and just lobbying mainstream politicians is a dead end.</P>
<P>I am a steward in my union of public-sector workers. The last time we got a cost-of-living raise was when we participated with other unions in a one-day statewide strike on May 24, 2001.</P>
<P>We need to build solidarity throughout our public and private unions, and organize demonstrations, pickets and strikes. Only this way can we stop the attacks on living standards, working conditions and union rights, and build a society based on human need, not corporate greed.</P>
<P><B>Patrick Switzer, </B>Seattle</P>

