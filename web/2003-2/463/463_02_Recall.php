<P><font face="Times New Roman, Times, serif" size="4">Politicians jockey for position before October 7 election</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The California recall circus</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Todd Chretien</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 8, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE CALIFORNIA recall circus got even wilder as an August 9 deadline for candidates to get on the ballot approached.</P>
<P>The right-wing sponsors of a petition drive to recall Democratic Gov. Gray Davis last month presented enough signatures to qualify for an election, which is now set for October 7. But as <I>Socialist Worker </I>went to press, Davis filed a lawsuit to postpone the vote until next March--and another to get his name on the second half of the ballot, among the field of perhaps 300 or more candidates from which voters would choose a successor for Davis if the "yes" votes win on the first half of the ballot.</P>
<P>Meanwhile, Davis and the California Democratic leadership caved in to Republican demands to cut an additional $3 billion from education, health care and social spending to close a $38 billion budget gap.  </P>
<P>Now Davis is trying save his skin by appealing to the same union members that he's axing--and calling himself a "progressive" for the first time anyone can remember. Not many people are buying it. Davis' approval rating sits around 20 percent, as thousands of state workers lose their jobs, tens of thousands of teachers take deep pay cuts, and health clinics close down.</P>
<P>Davis is so unpopular that even powerful California Democrats like Sen. Barbara Boxer and Rep. Loretta Sanchez have broken ranks to advocate for Sen. Dianne Feinstein to run on the second part of the ballot--something that would likely be a fatal blow to Davis if it happened.</P>
<P>Meanwhile, top Republicans are still maneuvering over the recall vote. So far, only representatives of the rogues' gallery of extreme right-wing Republican businessmen--including Rep. Darrel Issa (who paid nearly $2 per signature to get the recall on the ballot) and Bill Simon (who lost to Davis in last fall's regular election, despite spending millions of his own cash)--have announced they will run.</P>
<P>Tune in to the <I>Tonight Show With Jay Leno </I>this Wednesday, and you can watch potential candidate Arnold Schwarzenegger tell the world if he's running or not. The "moderate" possibility from this freak show is former Los Angeles Mayor Richard Riordan, but he hasn't announced yet.</P>
<P>If all this wasn't enough, former right-winger-turned-populist-pundit Arianna Huffington is considering entering the fray. She's supported by a group of Hollywood liberals and progressive activists who believe that her notoriety--and her huge personal fortune--could help her win the race on an "independent" platform.</P>
<P>Despite some good criticisms of Davis and the Democrats, though, Huffington says that she will not run if a prominent Democrat like Feinstein, enters the race--not exactly a principled "independent" position.</P>
<P>One candidate who has already said that he will run is the Green Party's Peter Camejo, who won 5 percent of the vote in last fall's election. Camejo would be well placed to lead the Green Party to its best-ever showing in a statewide race.</P>
<P>But he has said that he would drop out if Huffington runs. "It is very possible that one of us throwing our support to the other would generate additional support and defeat the Republicans," Camejo told Pacific News Service. "There's a high probability that I would be the one urging my supporters to vote for her." </P>
<P>This is unfortunate. A straight-up campaign by Camejo could contribute to building a real mass movement against the budget cuts, instead of focusing on electoral horse trading.  </P>
<P>The frenzied speculation about who will run will continue until the ballot deadline of August 9. Meanwhile, California's rich will enjoy their tax cuts--while the rest of us take it in the teeth.</P>

