<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">War lies exposed and growing anger over slumping economy</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Is Bushs's party over?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 1, 2003 | Page 3</font></p>
<P><font face="Times New Roman, Times, serif" size="3">PRESIDENTIAL APPROVAL ratings are falling, evidence of official lies about Iraq is mounting, and the economy is wobbling. Has George W. Bush's post-September 11 wartime popularity been punctured for good? And will it make a difference for working people fed up with the corporate agenda that Bush is driving forward?</P>
<P>Certainly the White House is on the defensive for the first time since the Enron crash and corporate corruption scandals exposed the inner workings of the CEOs that back Bush. The Iraq war drive, launched in earnest last fall, helped Bush to bury that issue--just as the White House used the September 11 attacks a year earlier to claim legitimacy for an unelected president and change the subject from the rotten economy.</P>
<P>Now the truth is catching up with Bush--not just the fabricated claims about Iraq's weapons of mass destruction to justify an imperialist war, but the mounting misery caused by the U.S. economic crisis.</P>
<P>A panel of economists recently declared that the recession officially ended two years ago. But you wouldn't know it to judge from the deteriorating living standards for working people.</P>
<P>According to a survey by researchers at Rutgers University and the University of Connecticut, one in five Americans were laid off between 2000 and 2003. The survey found that 43 percent of people are worried about job security--and just 8 percent think that Bush is doing an "excellent job" on job-related issues.</P>
<P>No wonder. Bush's economic policy is a one-size-fits-all approach--tax cuts for the rich to solve every problem. So when Bush traveled to Michigan in late July to tout his "jobs and growth" plan, he found plenty of skeptics in a state where the unemployment rate has hit 7.2 percent, the highest in a decade. And with 2.6 million jobs lost since March 2001, Bush could become the first president since Herbert Hoover from the Depression era to preside over a net decline in employment.</P>
<P>With a finger to the wind and an eye on the polls, Democratic presidential candidates are starting to take a tougher line against Bush--on the Iraq war lies and the economy.</P>
<P>Never mind that Democrats rolled over for Bush's two massive tax giveaways--and only started making noise about them afterward. And forget that the Democrats handed the White House a blank check for wars in Afghanistan and Iraq, as well as the repressive USA PATRIOT Act.</P>
<P>What's more, if the Democrats' anti-Bush rhetoric has sharpened, their policies have a lot more in common with the Republicans than anyone ever admits. Consider former Vermont Gov. Howard Dean, considered the liberal "outsider" in the Democratic presidential field. He wants to "save" Social Security by increasing the retirement age to 70.</P>
<P>Meanwhile, Democratic governors, from Gray Davis in California to Rod Blagojevich in Illinois, are coping with massive budget deficits by implementing vicious budget cuts--with devastating consequences for millions. In fact, U.S. politics has shifted well to the right over the past 25 years because of policies pursued by Democrats as well as Republicans.</P>
<P>That's why Corporate America can still act with such impunity--strangling health care reform, stealing billions with Enron-style rip-offs, attacking unions, and simply imposing cuts in wages and benefits for the nearly 90 percent of workers who have no representation.</P>
<P>There's only one way to resist these attacks--with organization, protest and struggle. This summer's big labor contract showdowns at Verizon and in the auto industry could become key battles.</P>
<P>Whether or not the resistance takes shape, small local battles are already taking place--to stop cuts in schools and social services, to oppose racist police brutality and more. Building these fights--not relying on the promises of this or that Democrat--is the way to oppose not only Bush, but the class war that's being waged against working people.</P>


