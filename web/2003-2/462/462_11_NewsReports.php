<P><p><font face="Times New Roman, Times, serif" size="4"><b>Palestine tour</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Monique Jeanne Dols</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--More than 100 people gathered at the Brecht Forum on July 12 to hear activists from Palestine speak about their struggle for freedom and justice. This was the New York City stop for activists on the "Road Map to Truth Tour" organized by the New York Solidarity Movement for a Free Palestine with Jews Against the Occupation, Al-Awda and The Brecht Forum.</P>
<P>Maha Nassar of the Union of Palestinian Women's Committees and the Alternative Information Center and Ala Azzeh from the Handala Center, a youth center in Beit Jibrin refugee camp, spoke on the current peace process and the significance of the U.S.'s support for Israel in the context of the U.S. war on terror.</P>
<P>Speakers emphasized that the current "peace process" had nothing to do with bringing peace, but only further legitimizes Israel's war against Palestinians. If the Oslo peace accords forced the Palestinians to give up their rights to freedom, the current road map is designed to make sure it stays that way.</P>
<P>Azzeh pointed out that the tactics of urban warfare that were used by the Israelis to suppress the uprising at the Jenin refugee camp in April 2002 are today being used in Iraq to suppress resistance to U.S. colonial rule. But it's not just the tactics of colonization that are being exported but also tactics of resistance.</P>
<P>"Iraqi resistance will become an example for the Palestinian people and oppressed people," Nassar said. "We need to be able to replace the globalization that oppresses people. We should replace this with the globalization of the intifada. The intifada of the poor people."</P>
<P>Look for the tour in your city.</P>

<P><p><font face="Times New Roman, Times, serif" size="4"><b>Madison fight for gay rights</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Katie Ray</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">MADISON, Wis.--Activists came out to rally on July 20 in the annual Gay and Lesbian Pride Parade in support of three city employees who were recently fired for being gay or "gay-friendly." The former employees have filed discrimination complaints with the State of Wisconsin Department of Workforce Development's Equal Rights Division.</P>
<P>Two were fired after being questioned by city Parking Manager Robin Williams about their sexual orientation. A third employee--a supervisor with more than 23 years of service--was fired after Williams concocted ways to harass the employee, who was openly friendly to the gay workers, and then had her fired.</P>
<P>Beyond these three former employees who have lodged formal complaints, an additional worker says that he was forced to take a position with another department because he is gay. Before transferring, he was denied six promotions by Williams. Gay and lesbian rights are being attacked in Wisconsin, and activists have to fight back.</P>

<P><p><font face="Times New Roman, Times, serif" size="4"><b>Boston stands up to gay bashing</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Mitch Day</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">BOSTON--"No more hate crimes, not ever!" That was the slogan of a 100-strong rally against gay-bashing on July 26 in response to an attack on a lesbian couple during a July 4 celebration in East Boston.</P>
<P>Lisa Craig, her partner and their two children were verbally harassed by a group of teenage girls, who later returned and brutally beat Lisa. She received more than 200 stitches to her head and has suffered partial facial paralysis.</P>
<P>In Massachusetts, more than 100 reported cases of gay-bashing go on every year. Protesters marched to the park where the couple was attacked. At an open mike rally, several people expressed anger at how the police handled the attack.</P>
<P>When police arrived on the scene, an officer was reported to have said to the assailants, "Get the fuck out of here before I arrest you." Lisa's partner later had to submit to a physical search. This rally points the way toward getting rid of gay-bashing--by creating a movement that challenges homophobia wherever it happens.</P>


