<P><font face="Times New Roman, Times, serif" size="5"><b>U.S. soldiers show growing anger at Bush</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 1, 2003 | Page 5</font></p>

<B><P><font face="Times New Roman, Times, serif" size="3">ERIC RUDER reports on the growing discontent among U.S. soldiers in Iraq.</P>
</B>
<P> "I FEEL very encouraged overall that conditions here are much better than I thought they were before I came." Those were the words of Deputy Defense Secretary Paul Wolfowitz--one of the Bush administration's leading hawks--during his trip to Iraq in July.</P>
<P>If Wolfowitz really believes this, then he should find out what "conditions" are really like for ordinary U.S. soldiers in Iraq. He should get out of his air-conditioned convoy and endure the 115-degreee heat while wearing a helmet and Kevlar flak jacket. And he should leave behind his special security forces and see what it feels like to live in constant anxiety of being attacked by an Iraqi resistance that shows no signs of going away.</P>
<P>For the 12,000 soldiers in the Army's Second Brigade, Third Infantry Division, who've been in Iraq since last September, the daily grind has become unbearable. Unlike Wolfowitz, they are on the receiving end of daily attacks by an Iraqi resistance fed up with an invasion force that promised to "liberate" them and improve their lives, but has refused to give up political control--while Iraqi living conditions have gone from bad to worse.</P>
<P>"Our morale is not high or even low," wrote an anonymous soldier in an e-mail that was widely circulated on the Internet. "Our morale is nonexistent. We have been told twice that we were going home, and twice, we have received a 'stop' movement to stay in Iraq."</P>
<P>Some soldiers have risked disciplinary measures to express their extreme bitterness at being repeatedly lied to. "I've got my own 'Most Wanted' list," a sergeant at the Second Battle Combat Team headquarters told an ABC reporter, referring to the deck of cards issued by the U.S. military with the 52 "most wanted" Iraqis. "The aces in my deck are Paul Bremer, Donald Rumsfeld, George Bush and Paul Wolfowitz."</P>
<P>One soldier said he felt like he'd been "kicked in the guts" when he heard that his return had been postponed indefinitely. "We're being told to stay by commanders who just got here a month ago," fumes Sergeant Ronald Black. "They haven't been here since fucking September like we have."</P>
<P>And Bush's "Bring 'em on" taunt in response to a question about armed Iraqi resistance has infuriated soldiers and military families alike. "May I just say, Mr. President, perhaps you truly do believe in the invincibility of our military," wrote one soldier's mother named Marticia. "However, the next time you invite attacks on my son, and others, kindly stand in front of our soldiers, rather than hiding behind."</P>
<P>From the beginning of the war on March 20 until Bush declared hostilities over on May 1, 139 U.S. soldiers were killed. Since May 1, 100 more soldiers have been killed--and counting.</P>
<P>In early May, Shaun Cunningham wrote to his parents about putting men who died in a helicopter crash into body bags. "I had blood all over me, and all I could think about was this guy's wife and kids who were in his wallet staring at me," wrote Cunningham. "The war is over? Yeah, tell that to these guys' families."</P>
<P>Despite all of these stories of anger and despair, military spokespeople deny that there's been a dip in morale. "The people on the ground really seem to want to stay there," said Pentagon chief financial officer Dov Zakheim. "Even the people I visited in hospital, their number one objective is to get back into theater."</P>
<P>Now, to add insult to injury, the Bush administration is cutting funding for programs that help overseas soldiers and their families. The White House has opposed increasing benefits--from $3,000 to $6,000--for families who lose a soldier in combat.</P>
<P>The administration plans to reduce monthly "imminent danger" pay, and Bush has cut funds for improving military housing. Between the lies told to justify a war and the Bush administration's blatant disrespect for the soldiers it orders around, the Pentagon brass are faced with a growing crisis caused by resentment and anger in the ranks.</P>



