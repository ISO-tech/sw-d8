<P><font face="Times New Roman, Times, serif" size="5"><b>Ashcroft's goons deport Rabih Haddad in secrecy</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 1, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">JOHN ASHCROFT'S witch-hunters sank to a new low in July when they secretly deported Muslim cleric Rabih Haddad to Lebanon. Haddad had spent the past 19 months behind bars--imprisoned on a minor visa violation after the Global Relief Foundation (GRF), the charity he co-founded, was accused of financially aiding al-Qaeda.</P>
<P>The Justice Department never released any evidence connecting Rabih--who lived in Ann Arbor, Mich., with his family--to terrorism. </P>
<P>The best that prosecutors came up was pictures of books that "glorify martyrdom through jihad"--plus pictures of the kind of hand-held radios that terrorists "commonly use." FBI agents found this "evidence" searching the GRF's trash.</P>
<P>But the Justice Department didn't need real proof--because under the USA PATRIOT Act, they were allowed to present "secret evidence" against Rabih and keep his hearing closed to the public. Thus, in the 19 months that Rabih was in jail, the Feds <I>never</I> charged him--or the GRF--with any crime related to terrorism.</P>
<P>"The government did not have a shred of evidence to support their innuendos," Mazen Haddad, Rabih's brother, told <I>Socialist Worker. </I>"If they had anything at all, even the slightest proof, they would have charged him with it." What Ashcroft's goons did do was harass and intimidate Haddad and his family at every opportunity--including threatening to deport his wife Salma Al-Rushaid and their children.</P>
<P>Rabih had requested political asylum in the U.S. &#151;because of fear of reprisals by authorities if he was returned to Lebanon. When immigration officials denied him asylum in November, Rabih's lawyers appealed to the U.S. Sixth Circuit Court of Appeals.</P>
<P>On Friday, July 12, the court rejected his appeal--because it was filed one day too late. By Monday, Rabih was on a plane to Amsterdam, and then to Beirut. </P>
<P>Rabih's family would never have known about his deportation--except for the fact someone broke the rules and allowed him to call his wife from Amsterdam. In fact, when reports of Rabih's deportation first broke in the <I>Ann Arbor News</I>, a government spokesperson initially asked the newspaper to hold the story for two days for "national security" reasons. By that time, Rabih could have been "disappeared" by Lebanese authorities.</P>
<P>In fact, on his arrival in Lebanon, the authorities detained Rabih--apparently at the request of U.S. officials. After several hours of interrogation he was released--thanks to the frantic efforts of his family and supporters. "The telephone call Rabih made from Amsterdam may very well have saved his life," said Mazen.</P>
<P>Rabih's wife and children are being allowed to join him in Lebanon, but their future remains uncertain. "[H]ow will he be able to support his family there?" asked Mazen. "Even if we assume he is safe for now, how will he be able to get a job? Who will want to employ someone the U.S. still insists has ties to terrorism? They have ruined his life and the wellbeing of his whole family...They haven't the slightest decency to admit they have been wrong and allow him to resume his life. They still haunt him with their accusations."</P>


