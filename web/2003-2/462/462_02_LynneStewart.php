<P><font face="Times New Roman, Times, serif" size="4">Terrorism charges dismissed</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Feds' witch-hunt of lawyer set back</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Peter Lamphere </b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 1, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">A FEDERAL judge last week dismissed terrorism charges against a prominent left-wing lawyer targeted by John Ashcroft and his witch-hunters. In April 2002, Ashcroft personally announced that his Justice Department was indicting Lynne Stewart for giving "material support" to a terrorist organization.</P>
<P>In reality, all Stewart--a New York City defense lawyer known for defending political prisoners--did was represent her client, Sheikh Omar Abdel-Rahman, the Egyptian cleric charged with masterminding a plot to blow up New York landmarks. Basically, Stewart was accused of delivering messages--through supposedly coded public statements--from the sheikh to Islamists in Egypt.</P>
<P>Lawyers and their clients are supposed to have special privacy rights protected by the Constitution. But John Ashcroft and his Justice Department want to chip away at this, using the excuse of September 11.</P>
<P>Stewart was their first major test case. But last week, Judge John Koeltl said that the two most serious charges against Stewart were unconstitutionally vague and revealed "a lack of prosecutorial standards."</P>
<P>"This was the product of a very broad movement from all sections of the ideological spectrum," Stewart told <I>Socialist Worker. </I>"We got tremendous support from coast to coast. Just seeing the people who packed the courtroom all the days we had hearings was enough to convince the judge that this is a not an isolated person, or someone who is easily scorned." </P>
<P>Stewart sees herself as "the very thin edge of a very large wedge," she says. "If they could have gotten me under the door, they could have done a lot of damage to the ability of defense lawyers to represent their clients." </P>
<P>However, the battle to defend Stewart isn't over. She still faces three other charges. If found guilty, she could spend up to 10 years in prison and have her law license revoked. Supporters plan to pack the Koeltl's courtroom again at her next hearing on August 26 at 9 a.m. in Manhattan.</P>


