<P><font face="Arial, Helvetica, sans-serif" size="2"><b>REVIEW</b></font><br>
<font face="Times New Roman, Times, serif" size="4">It's not "kicking ass." It's not irony...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>It's called sexism!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Lalasz</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 1, 2003 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEXISM IS alive and well--and getting worse in popular culture. There are any number of examples--from reality shows like <I>The Bachelorette,</I> to beer commercials depicting scantily clad women fighting each other, to the never-ending string of dating shows like <I>Blind Date</I> and <I>Shipmates.</P>
</I><P>Or look at the <I>Charlie's Angels </I>movie sequel <I>Full Throttle</I> that just opened in theaters. The three main characters--or "Angels," played by Cameron Diaz, Lucy Liu and Drew Barrymore--wear barely anything at all, but still "kick ass" in one gratuitous scene after another.</P>
<P>According to the <I>Angels' </I>twisted conception of female empowerment--Barrymore is the film's co-producer--scantily clad Angels steal a security pass while giving a lap dance, with another Angel doing a pole dance as an added distraction. Demi Moore plays the villain and appears in a beach scene in a skimpy bikini. She reportedly had four plastic surgeries to get in shape for the role.</P>
<P>Then there's the recently premiered <I>Stripperella</I> cartoon, found on the TNN cable channel--otherwise known as "America's First Network for Men." Former <I>Baywatch </I>regular Pamela Anderson makes her animation debut as a superhero who dances in a strip club. Despite Anderson's insistence that "It's harmless, it's only a cartoon," nothing could be further from the truth.</P>
<I><P>Stripperella</I>, created by Marvel Comics icon Stan Lee, who created the <I>Incredible Hulk </I>and <I>Spider-Man, </I>features a lot of cartoon cleavage and sexual innuendo.The show centers on buxom exotic dancer Erotica Jones, an Anderson-esque character who leads a double life as the masked superhero "Stripperella" (also known as "Secret Agent 69").</P>
<P>She's a stripper by night, a crime fighter by later at night. Using sex appeal to cloud the minds of male crooks, she comes equipped with special gadgets, such as her lipstick laser and wall-climbing stiletto heels.</P>
<P>Her breasts are natural lie detectors, and her legs are powerful weapons she wraps around the heads of her foes--in a move she calls the "scissor-ella." "I'm very proud of <I>Stripperella,</I>" Anderson says. "She's my alter ego--strong, smart and sexy and, let's face it, a bit of a slut." This quote encapsulates the new sexism perfectly--and the slippery slope it creates, opening the door for things to go even further in a blatantly violent direction.</P>
<P>Take "Hunting for Bambi." According this so-called game's Web site, men can pay $10,000 to track down and shoot naked Las Vegas show dancers with paint ball guns in the Nevada wilderness. Shortly after the media reported on "Hunting for Bambi"--provoking outrage from several women's organizations--it was reported that the game was all a hoax.</P>
<P>Despite this, the message is undeniable--it's open season on women.</P>
<P>These examples reflect what has become a never-ending onslaught of sexist images. Women continue to be objectified. Yet without feminists or left-wing activists offering a framework to explain this and challenge it, all that's left is confusion.</P>
<P>Women today face a contradictory position. They have definitely won some important advances--more women go to college, for example. This has led some to argue that sexism in our society is a thing of the past.</P>
<P>But this is wrong. Not only is it the case that women on average are still paid far less than men, but the corporate media and entertainment industry still make big bucks promoting the idea that women should be seen as sex objects. This is often dressed up as "irony" or "kitsch"--with the promoters of sexism responding to all criticism as if anyone who takes offense is just a humorless dolt.</P>
<P>Unfortunately, given the confused atmosphere, some people who oppose discrimination against women have tried to identify with some aspects of today's sexist imagery--as if sexy women "kicking ass" against men is a sign of women's power. But you can't counter the problem of sexism by mirroring women's objectification.</P>
<P>Sexism needs to be challenged head on, but in a different way--by rebuilding a new movement that takes up women's rights. History shows that this is the only way sexism has been pushed back and women have become more equal in society.</P>
<P>That had a real impact on many issues facing women, including their depiction in popular culture. We need a movement that recaptures that sentiment--one that takes up the fight against the Bush administration's attacks on women, from abortion to affirmative action. This is how women made the gains we take for granted now--and would begin to turn the tide against the new sexism.</P>



