<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A "humanitarian" invasion of Liberia?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 1, 2003 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">IN THE run-up to the invasion of Iraq, the Bush administration couldn't wait to launch its war for oil and empire. Today, with the African country of Liberia descending into chaos and millions facing death and disease because of civil war, the White House hesitates to get involved.</P>
<P>This double standard couldn't have been clearer last week as casualties--most of them civilians--mounted from fighting in the capital of Monrovia. Bush has dispatched a force of 2,500 Marines to wait off the Liberian coast--in support of Nigerian-led "peacekeepers" who are supposed to take over policing the capital after the country's dictator, Charles Taylor, flees into exile.</P>
<P>But the Marines won't arrive until early August--and the White House has so far refused to take further action. As Defense Secretary Donald Rumsfeld--the administration's most determined hawk when it came to Iraq--explained: "[I]t doesn't fit into the national strategic interests, as far as I can see them." </P>
<P>Many people who opposed Bush's war on Iraq are naturally outraged by this hypocrisy. But their calls to send U.S. troops on a "humanitarian intervention" to force an end to the carnage in Liberia are mistaken.</P>
<P>Rev. Jesse Jackson, for example, charged that the failure to intervene in Liberia reflected the racism of U.S. foreign policy. In one sense, Jackson is right. Since the Cold War against the ex-USSR ended more than a decade ago, the U.S. has shown little interest in Africa--even as living standards plunged and the AIDS epidemic ravaged the continent.</P>
<P>But in another sense, Jackson--and other supporters of "humanitarian" intervention--are wrong. Liberia's current crisis is a <I>product </I>of U.S. intervention.</P>
<P>After the military dictator Samuel Doe seized power in 1980, the Reagan administration showered him with arms and aid--because he signed up as a Cold War ally. The U.S. financed Doe's wars against rival warlords--one of which was Taylor.</P>
<P>When the Cold War ended, and the U.S. had no further use for Doe, Taylor seized power in 1990, and ruled by fear and torture throughout the decade--with tacit support from Washington. Only when the U.S. couldn't be sure that Taylor would guarantee "stability" did it turn to backing the rebels who are now laying siege to Monrovia.</P>
<P>Left-wing journalist Greg Palast acknowledges U.S. responsibility for creating the mess in Liberia--but insists that it is "obligated" to intervene. "We rushed in to fund the killings, now we must go in to end it," Palast wrote. "Until then, the Liberians will pile the corpses at our doorstep to remind us of the blood on our hands."</P>
<P>But supporters of intervention in Liberia should be careful what they ask for--because they might regret getting it. The last major U.S. "humanitarian" intervention in Africa, the invasion of Somalia in 1992, ended in disaster.</P>
<P>Most Americans remember that U.S. forces withdrew after losing 18 soldiers in a firefight in the capital of Mogadishu. But they forget--because the Pentagon suppressed the fact--that U.S. troops also killed or wounded an estimated 10,000 Somalis. Sold as an effort to feed starving people in Somalia, the U.S. intervention was really an attempt to install Washington's favorite warlord in power.</P>
<P>As the experience in Somalia shows, the Pentagon isn't a social work agency. If the U.S. sends troops into Liberia, it will do so to secure U.S. interests in West Africa--an area of increasing interest to U.S. oil companies. And history shows there is no limit to the barbarism that Washington will unleash in pursuit of its interests--whether the cover for the mission is "humanitarian" or not.</P>
<P>If the Pentagon can sell intervention in Liberia as "humanitarian," it becomes easier to sell intervention in places like Iraq as "liberation." That's why people concerned about the catastrophe in Liberia should not call for U.S. intervention--which would be certain to do more to bolster the Pentagon's image than to help ordinary Liberians.</P>


