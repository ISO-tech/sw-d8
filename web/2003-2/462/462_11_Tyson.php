<P><font face="Times New Roman, Times, serif" size="4">Labor activists in Wisconsin step up solidarity efforts</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Tyson strikers hold strong</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Bill Linville</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 1, 2003 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">JEFFERSON, Wis.--After almost five months on strike, workers at Tyson Foods are still holding strong. Out of 470 striking members of United Food and Commercial Workers (UFCW) Local 538 workers, only three have crossed the line, according to union representative Alyssa Rotschafer.</P>
<P>Tyson is out for blood. The company's contract proposal would set up a divisive two-tier wage scale, dramatically increase workers' health care premiums, eliminate pension benefits for new hires and freeze pensions for current workers. "The pension isn't good anyway, and now they want to freeze it," Primo Renteria, a 20-year veteran of the Jefferson plant, said of the insulting offer.</P>
<P>Solidarity efforts got a boost in nearby Madison on July 21 at the monthly South Central Federation of Labor meeting, where a resolution was unanimously passed in favor of setting up a strike solidarity committee. More workers showed their solidarity on July 1, when some 200 women from the Midwest School for Women Workers, sponsored by the state AFL-CIO, came to the picket line during shift change.  </P>
<P>Madison students and labor activists are stepping up the pressure at the University of Wisconsin (UW)-Madison, which purchases approximately $100,000 worth of Tyson products a year, to stop buying Tyson products. The UW student government recently passed a resolution condemning the university's use of Tyson products, and a meeting is being set up with decision makers in the UW purchasing department to hear the demands of activists and strikers.</P>
<P>Local activists scored a recent victory with the passage of a Dane County Board resolution ending the purchase of Tyson goods and the removal of frozen pizzas using pepperoni coming from the scab plant from several local co-ops.</P>
<P>Moreover, strikers must keep up the pressure on the picket line. Management is recruiting scabs from outlying areas at $10 an hour--$1 more than the starting wage under Tyson's proposed contract.</P>
<P>Tyson is trying to use divide-and-conquer methods to beat the strike. The company is bringing in scabs from outlying areas, including Beloit and Whitewater, many of whom are Black or Latino. The majority of strikers are white.</P>
<P>But union members are trying to educate scabs or potential scabs about the strike. Tom Rieck, who has worked at the plant for 24 years, says that he convinced the father of a poor, Latino family not to cross the line while he was picketing the local Best Western, where scabs were being recruited and put up by Tyson.</P>
<P>More organizing like this will be necessary if workers are going to force Tyson Foods back to the bargaining table. A rally is planned for August 17 at the plant gates in Jefferson. Come out and show your support.</P>



