<P><font face="Times New Roman, Times, serif" size="5"><b>New law requires taped confessions</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alice Kim</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 1, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ILLINOIS BECAME the first state last week to require police to audiotape or videotape interrogations of murder suspects while in custody. Gov. Rod Blagojevich--who said that he opposed videotaped confessions during last year's election campaign--signed the bill into law in response to growing public distrust of the state's criminal justice system. </P>
<P>Illinois has a scandalous record of sending innocent people to prison. Last January, former Illinois Gov. George Ryan emptied the state's death row by commuting all death sentences--and pardoned four condemned prisoners entirely, bringing the total number of innocent men freed from death row to 17. All four of the pardoned men were brutally tortured by Chicago police, and their alleged confessions were then used to convict and sentence them to death. </P>
<P>According to a study by the Center on Wrongful Convictions at Northwestern University Law School, 25 of the 42 wrongful murder convictions documented in Illinois since 1970 involved false confessions. "We strongly suspect that the greatest single cause of wrongful convictions is false confessions," said Rob Warden, the Center's executive director. </P>
<P>The videotaped confessions law stands in stark contrast to tough-on-crime measures touted by Republicans and Democrats alike for the past two decades. And Blagojevich is expected to sign related reform measures that would bar the execution of mentally retarded people, give defendants more access to DNA evidence and limit the number of crimes that could result in a death sentence, among other things. </P>
<P>But pro-death penalty politicians--like Blagojevich himself--could try to use the reforms to claim that the system has been fixed and prepare to restart executions. Some opponents of the death penalty have said that our movement can do no better than win reforms.</P>
<P>We shouldn't accept this. No reforms will change the racism and class bias of the death penalty, and none will ensure that the innocent won't be executed. The only solution is to abolish the death penalty once and for all.</P>


