<font face="Times New Roman, Times, serif" size="5"><b>What was left out of the 9/11 report</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 1, 2003 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ONE WAY to tell how much different the political climate is today from one year ago is to note the reaction of the Bush administration to revelations about the congressional report on the September 11 attacks released last week. President Bush's flaks issued a bland statement in his name: "Our law enforcement and intelligence agencies are working together more closely than ever and are using new tools to intercept, disrupt and prevent terrorist attacks."</P>
<P>This sounded like a compliment, especially following a nearly 900-page report that catalogued a series of bureaucratic screw-ups that cost the government numerous opportunities to unravel the September 11 plot. When some of the same tidbits appeared in May 2002, the administration went into overdrive to silence them.</P>
<P>Vice President Dick Cheney warned against imminent terrorist attacks, and Defense Secretary Donald Rumsfeld declared that countries like Iraq were developing weapons of mass destruction to hand out to terrorists like Osama bin Laden! All of that bluff and bluster worked to turn critical eyes away from the administration then.</P>
<P>But this kind of hot air is wearing thin today. Rumsfeld's worry about bin Laden getting his hands on Saddam Hussein's weapons of mass destruction looks pretty silly now that the U.S. hasn't found any evidence of those weapons after occupying Iraq for four months. Likewise, other parts of the report poke more holes in the armor that the administration has worn since September 11.</P>
<P>An essential part of the administration's defense last year was National Security Adviser Condoleezza Rice's contention that no one could have conceived of the use of hijacked planes as missiles. In fact, the September 11 report showed that the FBI and CIA documented al-Qaeda plans to do just that as far back as 1998.</P>
<P>The intelligence agencies that claimed they "didn't have the tools" they needed to "fight terrorism" should also have some explaining to do. In fact, the congressional report showed that the FBI had an informant tasked to two of the eventual hijackers in San Diego.</P>
<P>Yet in the fall of 2001, Ashcroft and the intelligence agencies lied--and a craven Congress swallowed their lies--to push through the biggest assault on civil liberties since the days of COINTELPRO.</P>
<P>That the report is not more damning toward the administration and its entire "war on terrorism" is a testament to the administration's ability to protect itself by refusing to declassify parts of report. "The most significant sets of events, in my opinion, are in the section of the report that has been censored and therefore won't be available to the American people," said Sen. Bob Graham (D-Fla.), the chair of the Senate Intelligence Committee when the September 11 report was prepared.</P>
<P>For example, an essential part of the administration's case for war in Iraq was the alleged ties between al-Qaeda and Iraq. But an earlier version of the report showed that U.S. intelligence believed there was no connection between Iraq and al-Qaeda, according to a source that spoke with a United Press International reporter.</P>
<P>The published version doesn't say anything about Iraq and al-Qaeda ties, but former Sen. Max Cleland (D-Ga.) charged the administration with stonewalling its publication so that it wouldn't undermine Bush's case for war in Iraq. Last year, Bush caught flak when news surfaced that he received an August 6, 2001, briefing on impending al-Qaeda attacks.</P>
<P>If Bush had been shown to fail to take action in the face of the impending disaster, his entire image as a decisive "wartime" leader would have been blown. We don't know what was in the briefing paper, because Bush successfully kept it out of the 9/11 report.</P>
<P>Another missing piece--in fact, an entire 28-page gap in the published report--documents connections between the U.S.-allied regime in Saudi Arabia and the 9/11 hijackers, according to those who have seen the uncensored report. But fingering the Saudis would have contradicted a central tenet of Bush's war on terrorism--that only designated U.S. enemies are considered "terrorists," but thugs like Israel's Ariel Sharon or Colombia's Alvaro Uribe are considered friends.</P>



