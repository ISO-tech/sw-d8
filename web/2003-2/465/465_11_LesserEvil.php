<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The politics of the "lesser evil"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 22, 2003 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE POSITION that we must vote in any Democrat in order to vote out the Republican depends upon an extremely narrow vantage point--that of comparing Bush in power with the rhetoric of his Democratic opponents who aren't in power. Finding a difference between the two, however small, is not difficult, and therefore the answer is perfectly clear: vote for the Democrat.</P>
<P>We should be clear about what this argument entails. It means voting for the electable Democrat, and the electable one will narrow his differences with the incumbent in order to appear just as "tough on national security," "tough on terrorism" and as a friend of big business.</P>
<P>The activist who plans to vote for a Democrat--any Democrat--might acknowledge this, but may then argue: at least we'll get something marginally better. To properly assess this argument, it is necessary to step back from the narrow framework in which it is posed, and look at the bigger social and economic picture.</P>
<P>Politics in the U.S. proves, wrote Frederick Engels in the 1880s, "that the bourgeois republic is the republic of capitalist businessmen, in which politics is simply a business." Elections are bought and sold in the U.S. to the highest bidders, and the government is one which is beholden, no matter which of the two parties is in power, to "capitalist businessmen"--that is the first thing to get right. This point is important both in terms of domestic and foreign policy.</P>
<P>For the peculiar character of U.S. politics historically is that it has been a two-party system, run alternately by two parties that are both servants of "capitalist businessmen," and where, as Engels wrote, "it appears as though every vote were lost that is cast for a candidate not put up by one of the two governing parties." The two-party system--the absence of any radical working-class or left political parties--is one of the secrets of the relative stability of bourgeois politics in the U.S. historically.</P>
<P>A refusal to build politically outside of this framework has doomed the left in the U.S. to the pathetically narrow choice posed at the beginning of this article: be realistic, hold your nose and vote for the lesser of two evils. But the history of the two-party system shows that electing the lesser evil has a two-fold effect.</P>
<P>It demobilizes mass struggle by putting our faith in the actions of bourgeois politicians. Secondly, if the lesser evil wins, his policies once in office become glaringly different from his campaign rhetoric.</P>
<P>The most famous case of this was Lyndon Johnson, who ran on a no-war ticket and ended up sending 500,000 troops to Vietnam. This was a particularly egregious case because it turns out Johnson had secret plans to send more troops to Vietnam before he won the 1965 election.</P>
<P>The labor unions in California who are supporting Gov. Gray Davis against his recall do not even have this excuse: a defense of Davis is a defense of a pro-business job, service and pension killer who has already "proven" himself in office. </P>
<P>The two parties represent the liberal and conservative wings of the same capitalist class. What unites them--their commitment to serving the interests of capital--are far more important than their differences. That is why both Clinton and Bush support neoliberal free trade policies. That is why Clinton ended up pushing through Republican-style welfare reform. </P>
<P>September 11 provided the ruling class with an opportunity to assert U.S. power more aggressively in a post-Cold War world, no matter which political party was in power. Moreover, to the extent there were any differences between Democrats and Republicans on the "war on terror," they have been tactical rather than fundamental.</P>
<P>Both, in the final analysis, are committed to the projection of U.S. power abroad. What policies different presidents have pursued, therefore, have been influenced more by the general political interests at any given period of the ruling class on the one side, and the level of mass struggle and pressure from below, on the other.</P>


