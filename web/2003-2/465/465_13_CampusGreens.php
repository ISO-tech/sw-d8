<P><font face="Times New Roman, Times, serif" size="4">Campus Green activists lean toward Kucinich</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Is this an alternative?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 22, 2003 | Page 13</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>Dear <I>Socialist Worker,</b><br>
</I>Should the Greens repent for their support of Ralph Nader in the 2000 presidential election--and line up behind a Democratic candidate? That was a key question running through the weekend as about 100 student activists from around the country gathered in Austin, Texas, in early August for the Campus Greens' national convention.</P>
<P>Nowhere was this more so than at an evening Super Rally, where Rep. Dennis Kucinich (D-Ohio), who is running for the Democratic Party presidential nomination, took the stage to ask for the support of Greens.</P>
<P>The issue emerged earlier, at a talk by former Rep. Cynthia McKinney, one of a handful of lawmakers to oppose George W. Bush's "war on terrorism," and who is now considering running for president on the Green ticket. But McKinney stressed that the main goal must be to get Bush "out of the door in 2004"--and that the Greens would have to find a way to build their influence without threatening the chances of the Democratic Party candidate.</P>
<P>Texas Green Party activist David Cobb, who is also seeking the nomination to be the Greens' presidential candidate, spoke out for a strategic voting campaign--where voters would be encouraged to support the Green candidate only in states where their vote wouldn't change the expected outcome between Bush and the Democratic nominee.</P>
<P>Cobb echoed what many people said during the weekend--that if Kucinich or Rev. Al Sharpton were nominated to run on the Democratic ticket, the Greens should drop their candidate altogether. This is an incredible statement from a group that rightly resisted all calls to vote for a Democrat when Ralph Nader was running for president.</P>
<P>It's not hard to see why Kucinich has support among Greens. At the Super Rally, he brought the crowd of 500 to its feet several times, blasting Bush's war--though he stopped short of calling for an end to the occupation of Iraq--and promising that his first acts as president would be to cancel NAFTA and the World Trade Organization.</P>
<P>This has swayed some people--like, for example, Michael Albert of <I>Z </I>magazine, who declared in his Super Rally speech that if Kucinich got the Democratic nomination, he would go "all out" in supporting him. But this ducks the real question. Kucinich doesn't have a chance of winning the nomination. </P>
<P>And he's part of a long history of liberal Democratic candidates--like, for example, Rev. Jesse Jackson--whose role has been to win support from the left and lead them back into the arms of the Democratic Party, with its corporate agenda. What was missing from the weekend was a clear-cut call to focus on building an independent third-party alternative to what Ralph Nader called the two-party duopoly.</P>

<B><P>Mike Corwin</B>, Austin, Texas</P>

