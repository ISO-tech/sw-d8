<P><font face="Times New Roman, Times, serif" size="4">Bush administration wants revenge on human shields</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Fined for resisting Washington's war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 22, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IF THE Bush administration gets its way, antiwar activists will have to pay fines totaling tens of thousands of dollars. All for the "crime" of trying to protect innocent Iraqis from the Washington war machine. The Feds are going after activists who traveled to Iraq before the start of the war to act as "human shields"--hoping that the U.S. would be less likely to bomb key targets in Iraq if U.S. and international activists were in the area.</P>
<P>Retired schoolteacher Faith Fippinger was one of several human shields who stayed near an oil refinery in Baghdad. When she returned home from Iraq on May 4, there was a letter waiting from the Treasury Department. The letter said that she was in violation of federal law because she had crossed the Iraqi border before the war--a violation of U.S. sanctions that prohibited American citizens from engaging in "virtually all direct or indirect commercial, financial or trade transactions with Iraq." </P>
<P>Fippinger says that the government has informed her that she will be fined "at least" $10,000--and as much as $270,000. But since she will refuse to pay, she could face up to 12 years in prison.</P>
<P>Fippinger wrote back, telling the Treasury Department to "please be aware that I will not contribute money to the United States government to continue the buildup of its arsenal of weapons." Now, the Feds say that if she doesn't pay the fine, it will increase, and the money will be drawn directly from her retirement paycheck, her Social Security check or any of her assets. </P>
<P>Judith Karpova, a 58-year-old writer from Hoboken, N.J., who also stayed near the oil refinery, told the <I>New York Times</I>, "We went there to protect innocent civilians, and I went there to protect my own country against further crimes against humanity and war crimes."</P>
<P>The Bush administration is also targeting the anti-sanctions group Voices in the Wilderness, demanding $20,000 in fines that were imposed last year for trips the group made back in 1998. Voices is also refusing to pay, saying that it will collect the $20,000 and use it to provide humanitarian assistance to people in Iraq.</P>
<P>The Bush administration has the nerve to claim that these fines aren't politically motivated. "Unlike in Iraq under Saddam Hussein--where dissent was met with imprisonment or worse--the freedom to protest and disagree with the government is a cornerstone of American democracy," Treasury spokesperson Taylor Griffin claimed. "However," he added, "the right to free speech is not a license to violate U.S. or international sanctions."</P>
<P>By that logic, Iraqi citizens should be able to fine the Bush administration for bombing their country in violation of international law. "We believe [the fines are] a violation of our First Amendment rights for freedom of religion, and also our freedom of speech rights as citizens who wish to make a very public statement that the international sanctions upheld by the United States and the United Nations were a violation of the most vulnerable people in Iraq," John Farrell of Voices in the Wilderness told <I>Socialist Worker.</P>
</I><P>"It was a way of making them suffer for the political and economic bargaining that other countries were doing over Iraq's resources. The United States is not spending the money to clean up depleted uranium in Iraq, not spending money to provide for the humanitarian needs and the security of Iraq, and the occupation is becoming more and more violent.</P>
<P>"And the United States is choosing to spend its resources and time and energy on prosecuting Voices in the Wilderness? That's definitely politically motivated, I think...Whatever happens in this case, we're going to continue our campaign of truth-telling and community-building and nonviolent resistance to U.S. foreign policy, and the military and economic violence that it entails."</P>


