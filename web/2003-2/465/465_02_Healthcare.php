<P><font face="Times New Roman, Times, serif" size="5"><b>Thousands of doctors call for national health care</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 22, 2003 | Page 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3">THOUSANDS OF U.S. doctors have endorsed a plan calling for a national health care system that would eliminate for-profit hospitals and insurance corporations. The proposal appeared in an article published in <I>Journal of the American Medical Association, </I>the prestigious publication that normally limits itself to scientific articles.</P>
<P>Four members of Physicians for a National Health Program--an organization that has long pushed for universal health coverage--wrote the article. But it gained media attention because it was endorsed by nearly 8,000 doctors, including two former surgeons general for the U.S. government.</P>
<P>The article points out that Americans spend $1.6 trillion on health care--more than enough to cover every single person in the U.S. if health care was organized under a national "single-payer" system. Instead, under the current system, hundreds of billions of dollars are siphoned off as profit by the health care bosses, says the article.</P>
<P>The authors criticize former President Bill Clinton's compromised health care-reform plan for leaving the for-profit insurance system intact. Instead, under the doctors' plan, people would be covered under a national health care budget, that would provide a set amount of money to hospitals for day-to-day operations and major expansions, pay for nursing home and home care for the elderly and develop a national list of drugs that the program would pay for. </P>
<P>Essentially, the government would pay for health care through an expanded and better-funded version of Medicare, the government health insurance program for the elderly. The proposal calls for increased taxes, but except for the very wealthy, say the doctors, this would be offset by the elimination of insurance premiums and out-of-pocket copayments and deductibles.</P>
<P>"Let the debate resume," said <I>Journal </I>editor Dr. Catherine DeAngelis, explaining the decision to publish the article. "It's sort of been on the back burner and it's time we get on the stick with this. We are the only developed country in the world that doesn't have a specific health plan for our people. It's a disgrace." </P>


