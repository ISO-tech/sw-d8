<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Activists debate what position to take in the 2004 election</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Should we settle for the "lesser evil"?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 22, 2003 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">"REALISM" IS the word of the day as many people on the left discuss what to do about the 2004 presidential elections. For some, realism means dropping a commitment to building a third party alternative in order to support the most "progressive" Democrat running for the nomination, Rep. Dennis Kucinich (D-Ohio).</P>
<P>For others, it means bypassing Kucinich for the more "electable" Howard Dean, the supposed "straight-talking maverick." A few others think that even Dean is too left wing to be "realistic"--despite a record as governor of Vermont that once won him the support of the Democratic Leadership Council, the Democratic Party's conservative, pro-business wing.</P>
<P>All these are stops on the same slippery slope.</P>
<P>The monster looming behind each of these positions is George W. Bush. Bush has to be defeated in 2004, goes the argument--and whether by hook or by crook doesn't matter.</P>
<P>It's understandable why anyone who cares about peace or justice would feel this way. Bush's presidency has been a disaster from day one. But the Bush fear factor is leading some people to wrong-headed conclusions about the elections.</P>
<P>Michael Albert of ZNet, a longtime advocate of an independent political alternative, recently urged activists to "vote smart. Vote for impact"--echoing the increasingly fashionable idea of strategic voting, in which supporters of a left-wing third party like the Greens vote instead for the Democrats in states where the Green candidate could tip the election to the Republicans. "Think about election night," Albert wrote. "Think about watching the returns. Think of your heart and soul's reaction if Bush wins."</P>
<P>The "safe state" strategy was advocated earlier this month at the national convention of the Campus Greens--where the star attraction at the concluding Super Rally was none other than Kucinich. Virtually every speaker agreed with the idea that Greens should vote Democratic if Kucinich somehow won the presidential nomination.</P>
<P>These are examples of activists allowing the fear of Bush to obscure the realities of the U.S. political system. History shows that the Democrats can't be trusted to provide a real alternative to the Republicans.</P>
<P>The Democrats rely on their reputation as the "party of working people" to win votes at every election. But no matter how seemingly opposed to the Republican agenda they are during the campaign, once in office, the Democrats pursue the same corporate agenda, if in a gentler form. This is because the Democrats are <I>not </I>a "party of working people"--but the second party of Corporate America.</P>
<P>This goes for "good Democrats," too. Liberals like Kucinich have an important role to play for the Democratic Party. Rather than pulling the party to the left, they pull the left into the party--winning support from the Democrats' liberal base, and then delivering that to whatever candidate gets the presidential nomination. </P>
<P>Thus, in 1984 and 1988, Rev. Jesse Jackson Sr. won the support of many people looking for a more progressive alternative to the status quo Democrats. But in the end, Jackson did the "realistic" thing and threw his support behind the moderates Walter Mondale and Michael Dukakis.</P>
<P>The principled commitment to build a political alternative independent of the Democrats that represents the interests of working people can't be reduced to a tactic--to be abandoned under "special circumstances" in favor of whatever Democrat is judged the most "realistic" candidate to beat Bush. What's missing in all the calculations about the 2004 elections is the question of protest from below.</P>
<P>Out of a sense of desperation at what the Bush administration has gotten away with, some people are concluding that who sits in the White House is the most decisive question in U.S. politics. This isn't true. </P>
<P>From civil rights for African Americans, to the struggle against war, to labor's organizing drives, to a woman's right to abortion, our movement has won its victories not because of friendly politicians, but because ordinary people fought to win them.</P>
<P>As the veteran activist and historian Howard Zinn puts it: "There's hardly anything more important that people can learn than the fact that the really critical thing isn't who is sitting in the White House, but who is <I>sitting in--</I>in the streets, in the cafeterias, in the halls of government, in the factories. Who is protesting, who is occupying offices and demonstrating--those are the things that determine what happens." </P>
<P>Our fight against Bush and his agenda has to remain focused on building struggle from below--not putting our hope in a Democrat. Now <I>that's </I>the realistic approach.</P>


