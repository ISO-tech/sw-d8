<P><font face="Times New Roman, Times, serif" size="4">Film exposes Catholic Magdalene asylums</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Profiting off of "salvation"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Afsaneh Moradian</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 22, 2003 | Page 11</font></P>
<B><I><P><font face="Arial, Helvetica, sans-serif" size="2">The Magdalene Sisters, </B></I>written and directed by Peter Mullan, starring Geraldine McEwan, Annie-Marie Duff, Nora-Jane Noone</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE CATHOLIC Church has a growing pile of skeletons in its closet. To the growing accounts of pedophilia and rape, you can now add asylums where tens of thousands of women were forced to work as indentured servants cleaning laundry for the Church.</P>
<P>In the new movie <I>The Magdalene Sisters, </I>writer and director Peter Mullan exposes these institutions that were hidden from public life. He describes the Magdalene Asylums as "prisons that weren't exactly prisons, filled with 'criminals' who hadn't done anything wrong."</P>
<P>Women were placed, by their fathers and local priests, in Magdalenes for such "crimes" as having premarital sex, giving birth out of wedlock, being raped and flirting with boys. The philosophy behind the asylums was that these women could only find salvation through vigorous work in the laundry and meager living.</P>
<P>The <I>Magdalene Sisters, </I>set at one asylum in Ireland in 1964, clearly shows how torturous life in the Magdalenes was. Subject to verbal and physical abuse at whim, the women wake at 6 a.m., eat and pray for half an hour and then work in the laundry until dark.</P>
<P>While a family member or priest could freely escort someone out of a Magdalene, women could not leave on their own, and most stayed until they died. The movie reveals that even though it was possible to escape, many women accepted their treatment because they believed they had committed a mortal sin and would go to hell unless they were saved.</P>
<I><P>The Magdalene Sisters </I>focuses on three women who are determined to escape the Magdalene. The three see through the nuns' abuse and threats and recognize that they are there, not for salvation, but to wash laundry.</P>
<P>As Bernadette (played by Nora-Jane Noone) says, "The nuns don't care about you. The nuns only care about getting the work done." The film makes brutally clear that the abuse these women faced in the name of religion and salvation was only a cover for the Church's own immense financial gain.</P>
<P>When head nun Sister Brigette (played by Geraldine McEwan) explains to three new girls why they have been sent to the asylum, we hear her voice but see only her fingers counting money. In the name of Catholicism, women were starved, caned, humiliated, raped and had their children and their lives stolen from them.</P>
<P>Mullan made the film in the hopes that the Church own up to its abuses. The Magdalenes only just closed down in 1996, and since then, no reparations have been offered to the women or their families.</P>
<P>Before the movie was released, the Catholic Church had not issued a single acknowledgment or apology for the horrors thousands of women endured. It's estimated that there were between 15 and 20 Magdalenes in Ireland. The asylums also existed in Scotland, England, Canada and the U.S. as well.  </P>
<I><P>The Magdalene Sisters</I> comes at an important time when much attention is being paid to the treatment of women in the name of Islam. As Mullan said recently at a screening in New York City, "[We should] look at what we're doing in our own backyard before we go throwing stones at others."</P>


