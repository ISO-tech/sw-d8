<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><br>
<font face="Times New Roman, Times, serif" size="4">California political establishment has no answers</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Can we recall the system?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 22, 2003 | Page 6</font></P>

<P><font face="Times New Roman, Times, serif" size="3">BARRING A court ruling postponing it, California will hold an October 7 election that will determine if Democratic Gov. Gray Davis is recalled--and replaced with one of 135 candidates. The almost unprecedented recall--only one other governor has ever been recalled in U.S. history--has generated political interest around the country, in large part because the field of candidates running to replace Davis includes right-wing actor Arnold Schwarzenegger, among a number of other crackpots. </P>
<P>In the midst of the news coverage focusing on the recall "circus," it's easy to lose sight of the bigger picture. Three points stand out.</P>
<P>First, the recall grows out of the California budget crisis and the inability of the state's political establishment to solve it. Davis, elected in a landslide in 1998, saw his popularity collapse in 2001, following his dithering response to the California power crisis. </P>
<P>Rejecting calls even among Democrats to take strong action against the power monopolies, Davis instead committed the state to spend upwards of $12 billion in long-term contracts to purchase power. At the same time, the popping of the dot-com bubble severely cut into California's tax income as cash from the dot-com millionaires dried up. </P>
<P>As the state's revenues dropped, the political establishment tried to evade responsibility. Faced with the likelihood of massive spending cuts, the Republicans and Democrats have tried to protect their pet constituencies and corporate contributors, at the expense of state spending on health, education and other essential services. </P>
<P>Second, both of the contending forces driving the recall--the Republican and Democratic Parties--want to push the crisis onto the backs of California's workers. These two parties' positions crystallized in the six-month budget standoff that ended in July. </P>
<P>Davis proposed increasing taxes on the working class--increasing the car tax, as well as taxes on alcohol, cigarettes and other products--to hold down the billions in cuts that he proposed. The Republicans proposed more draconian cuts, but opportunistically opposed any tax increases.</P>
<P>In the end, the budget that Davis signed slashed services massively and actually accepted increased spending on GOP priorities, such as rural law enforcement. Neither party challenged the corporate tax breaks, giveaways to energy companies, the massively regressive tax system, or the prison-industrial complex that are the real sources of the state's crisis.</P>
<P>Third, the recall shows how clearly corporate and wealthy individuals' money has corrupted the machinery of bourgeois democracy. In the last 10 to 20 years, the massive polarization of wealth in the U.S. has eroded what limited procedural reforms that bourgeois democracy had provided. </P>
<P>Under the system of legalized bribery through which political parties and campaigns are financed today, corporations and the rich have managed to take a commanding advantage over labor and ordinary people.</P>
<P>State governments approved initiative and recall provisions in the early 20th century as "direct democracy" reforms against corporate-controlled state governments. But in the last 20 years, these have been increasingly subject to manipulation by special interests and the political parties.</P>
<I><P>Washington Post</I> pundit David Broder described the "million-dollar question" for consultants who draw up initiatives and referenda: "Where's your million dollars?" There's no doubt that the recall of Davis would not have made the ballot had Rep. Darrell Issa not poured in up to $2 million of his own money into it.</P>
<P>This no reason to abolish citizen initiatives, recalls and referenda, as some insider pundits have urged. But it shows why reforms are needed to make sure that they don't simply become another weapon in the arsenal of the already well-connected.</P>
<P>The recall election holds out the possibility of catapulting a genuine progressive, like the Green Party's Peter Camejo, into the governor's chair. But much more likely, the winner will be one or another politician representing the corrupt political establishment that got California into the mess it faces today.</P>
<P>Whether the next governor of California is a Republican or a Democrat, he or she will be faced with the same budget crisis. Only by building a movement of working people from below will we be able to push back their continuing efforts to offload the state's crisis onto the working class.</P>


