<P><font face="Times New Roman, Times, serif" size="5"><b>Airport blacklist for antiwar activists</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 22, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">REMEMBER THE airport blacklist for antiwar activists? The one that the Feds claimed didn't exist? Turns out they were lying. And now they've been forced to admit it.</P>
<P>For months, antiwar activists have been saying that they were being flagged at airports for increased security checks after their names popped up in a database, supposedly for "known terrorists or enemies of the United States." Federal officials swore up and down that it was all coincidence--and the problem was that people with the similar names as legitimate security risks were being held up as "false positives."</P>
<P>But earlier this month, the Transportation Security Administration (TSA) was finally forced to admit--after the American Civil Liberties Union (ACLU) filed a Freedom of Information Act request--that it has at least two lists: one that includes terrorist names and aliases, and another that includes the names of at least some antiwar activists.</P>
<P>Under the ACLU's request, the TSA admitted that Rebecca Gordon and Jan Adams, who work for <I>War Times</I> newspaper, were held up at an airport last year because their names were on the second list. "Many ordinary anonymous individuals, especially immigrants and people perceived as Muslim, move about the U.S. carrying the fear that some government agent will swoop down and detain them without reason," Gordon and Adams said in a recent statement.</P>
<P>"We are lucky enough, as <I>War Times</I> organizers and experienced activists, to be able publicize and fight back when we receive a lightweight version of that treatment. Peace activists need to continue to monitor and challenge the U.S. government not only when it makes war abroad, but also when it attacks basic rights of the people in this country."</P>
<P>The government isn't even embarrassed by this revelation. In fact, under proposed guidelines for the TSA's Computer Assisted Passenger Pre-Screening System (CAPPS II) airport spying system, the government would be able to rifle through personal information about anyone getting on an airplane--including health or credit records. Passengers would then be assigned a "threat rating" of red, yellow or green, and subjected to heightened security--and harassment--based on their rating.</P>
<P>"Not only does the current proposal still keep the door open to broad snooping into our personal records, it expands the scope of the program beyond terrorism--a clear case of mission creep," said Jay Stanley, communications director of the ACLU Technology and Liberty Project. "If CAPPS II is allowed to go forward, it will set us on a path to a society where it's okay for the government to comb through your life, even when you've done nothing wrong."</P>

