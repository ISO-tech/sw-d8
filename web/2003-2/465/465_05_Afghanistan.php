<P><font face="Times New Roman, Times, serif" size="5"><b>Will the "new Afghanistan" fall apart?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 22, 2003 | Page 5</font></p>

<P><font face="Times New Roman, Times, serif" size="3">THE BUSH administration claims that its occupation of Iraq will make the country more democratic. But you only have to look at its last war to see what a U.S. military "victory" really means for ordinary people.</P>
<P>Afghanistan is a country that is ripping apart at its U.S.-sewn seams. The Washington-installed government of President Hamid Karzai has, from the start, been a coalition of warlords and thugs--many with records of human rights abuses that rival the worst of the Taliban era.</P>
<P>Last week, a new round of violence broke out, with more than 60 people killed in fighting and bombings in a single day in southern and eastern Afghanistan. Days later, another 22 were killed when a group of 400 guerrillas attacked police headquarters in the town of Barmal in the Paktika province, 125 miles southeast of Kabul.</P>
<P>It's no wonder why this kind of violence is breaking out. After bombing the country to pieces and promising to restore democracy and order, the Bush administration abandoned Afghanistan--failing to deliver any significant economic aid.</P>
<P>"Afghanistan is still on the critical list, and its recovery is being hampered by the failure of the international community to provide long-term political and financial support to the justice sector," said Amnesty International recently. The new Afghan police force routinely resorts to torture, and corruption among judges is widespread, says Amnesty. </P>
<P>And U.S. claims that the fall of the Taliban would liberate Afghan women have proven utterly hollow. "The system is failing to protect victims of rape, domestic violence and forced underage marriage," the human rights group reports. "[I]n some areas, the police randomly pick up girls and women and subject them to forced virginity tests." </P>
<P>The Bush administration has promised $1 billion in aid to Afghanistan this year, but with mounting occupation costs in Iraq, only a fraction of this is likely to show up. Meanwhile, ordinary people in Afghanistan will pay the real price of Washington's war for domination.</P>


