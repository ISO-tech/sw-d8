<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The challenges facing our unions</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 22, 2003 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">UNION LEADERS have a Labor Day message amid organized labor's worst crisis in decades. Avoid confrontations with the employers and throw everything into backing an "electable" Democrat to beat George Bush in the next presidential election. Again.</P>
<P>Twelve years ago, Lane Kirkland, then the head of the AFL-CIO, sounded the exact same theme as unions geared up to help throw the first Bush out of office. A labor-friendly Democrat, the argument went, would allow labor to reverse its decline, from representing more than 30 percent of the workforce in the 1950s to just 15.7 percent in 1992.</P>
<P>But once in office, Democratic President Bill Clinton proceeded with the corporate agenda--including the NAFTA trade agreement that labor opposed, while the promised reform of the U.S. health care system collapsed in a Democrat-controlled Congress. Today, union membership has declined further still under AFL-CIO President John Sweeney, to 13.2 percent--and just 8.5 percent in the private sector.</P>
<P>Unions are being pounded with job losses and concessions, which total tens of billions of dollars in the airlines alone. There's been little resistance on the picket line. Major strikes and lockouts in 2002 were at their lowest level since records were first kept in 1947--and figures for this year could be lower still.</P>
<P>Instead, labor leaders are again pinning their hope on the Democrats as an alternative to George W. Bush's anti-union policies. Labor's political machine is effective at turning out the vote. Union households accounted for one in four voters in the 2000 and 2002 elections.</P>
<P>Yet because the unions outsource their political message to "electable" candidates--sometimes including Republicans, as in the 2002 New York governor's race--workers have no independent political voice. And union leaders are even more willing today to water down their demands for the sake of getting a Democrat in office.</P>
<P>As Harold Schaitberger, president of the International Association of Fire Fighters, told reporters, "The stakes are so high and the risk is so great that it comes down to what candidate has the best chance to win. I just don't believe that we can allow our hearts and past performance to overtake our need to make a decision that can take us to the White House."</P>
<P>The result is that labor's strategy is the worst of both worlds: rolling over while Corporate America extracts massive concessions, and throwing resources into elections where its own agenda becomes subordinate to that of a "realistic" Democratic candidate.</P>
<P>There is a way that labor <I>could </I>put forward an agenda for working people--by leading a fight for what working people need. The 1997 strike at United Parcel Service galvanized the country because millions of working people identified with the struggle of part-time workers for decent pay and job security.</P>
<P>Today's months-long strike at a Tyson Foods plant in Wisconsin should also be made a symbol of workers fighting corporate greed--and backed by solidarity from the entire labor movement.</P>
<P>The contract standoff at the hugely profitable telecommunications giant Verizon should also be a rallying point for the whole union movement, as 78,000 union workers fight for job security and health care. Verizon's unions won strikes in 1998 and 2000 over similar issues. Today, however, union leaders have hesitated, ordering members to work without a contract even after they voted to authorize a strike.</P>
<P>Battles between labor and Corporate America are inevitable--aggressive employers will see to that, no matter how hard union officials try to avoid a showdown. There's no shortcut. Labor's decline can only be reversed by struggle.</P>
<P>Arguing for a new direction for our unions will be difficult as pressure to elect "anybody but Bush" continues to grow. But as experience shows, there's no other way to rebuild the labor movement.</P>


