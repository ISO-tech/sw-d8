<P><font face="Times New Roman, Times, serif" size="5"><b>The terrible price of their injustice system</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 15, 2003 | Page 5</font></p>

<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</B></I> </P>
<P>At a time when unemployment is rising and state and federal budgets are being drastically cut, the Department of Justice detailed the inevitable result of such actions in its annual report "Prisoners in 2002." According to the report, the prison population grew to 2,033,331 people last year. But the sheer numbers of people in the system don't tell the whole story. </P>
<P>The other statistics are just as startling. One in every 143 U.S. residents was in prison or jail at the end of 2002. Since 1995, the total number of male prisoners has grown 27 percent; the number of female prisoners 42 percent.</P>
<P>As usual, the brunt of the prison system fell on Blacks and Latinos. At the end of 2002, 10.4 percent of Black males aged 25 to 29 were in prison--compared to 2.4 percent of Latino males and 1.2 percent of white males in the same age group. Among the more than 1.4 million sentenced inmates at the end of 2002, an estimated 442,300 were Black males between the ages 20 and 39. </P>
<P>Black inmates represented an estimated 45 percent of all inmates with sentences of more than one year. Latino men were imprisoned at 2.6 times the rate of white men, and Latinas were imprisoned at twice the rate for white women. </P>
<P>The growth of the prison population has come at a huge price for working people throughout the U.S. As Jason Ziedenberg, director of policy and research for the Justice Policy Institute, states, "As legislators are struggling to fund education, health care, and stave off spending cuts, many are continuing to choose to pay for an expensive justice system that damages communities, and does not produce safe, healthy neighborhoods." </P>
<P>Instead of following the same tired path of the politicians who are hell-bent on cutting education and other social services, we need to fight for programs that will help people--and fight against any program or cut that will only land people in jail or prison.</P>
<B><P>Jim Bullington, </B>Denver</P>

