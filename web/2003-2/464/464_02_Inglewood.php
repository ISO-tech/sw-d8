<font face="Times New Roman, Times, serif" size="5"><b>Inglewood cops let off the hook</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 15, 2003 | Page 2</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>LAST SUMMER, a videotape of the police beating of 16-year-old Donovan Jackson-Chavis in Inglewood, Calif., showed the world the ugly face of racist police brutality. And late last month, the Inglewood cops who beat Donovan, Jeremy Morse and Bijan Darvish, got away with this crime--just like the four Los Angeles cops who savagely clubbed Rodney King in 1991.</P>
<P>Darvish, who lied to cover for his partner, was acquitted of filing a false police report. Morse--who was captured on videotape slamming Donovan onto the trunk of a car and slugging the handcuffed youth in the face--was charged with assault, but the jury deadlocked on this count.</P>
<P>And no wonder! While Inglewood's population is overwhelmingly Black, the jury had only one Black member. "I'm outraged," said local activist Najee Ali of Project Islamic Hope. "These officers were both guilty, and they should have been found guilty. It's a slap in the face.  It's Rodney King all over again."</P>
<P>Mitchell Crooks, who shot the videotape, explained, "I'm not surprised by the outcome at all. I knew that the prosecution was blowing the case. They didn't ask me to testify. They didn't ask the 20 people on the videotape to testify." Instead, the prosecutor let his whole case rely on the testimony of a police "expert"--who changed his story while testifying and said that he didn't think Morse was guilty of brutality!</P>
<P>Had the prosecutor called any of the eyewitnesses, they could have told the jury that Donovan didn't resist the officers--who were harassing Donovan's father about an expired driver's license. They could have testified that Donovan was slammed to the pavement and choked by a silver chain until he was unconscious. They could have testified that one of the cops called Donovan a "nigger."</P>
<P>Of course, this wasn't just incompetence. Prosecutors depend on building a close relationship with the cops. Activists are rightly calling for Los Angeles District Attorney Steve Cooley to retry Morse--and make sure his office doesn't bungle the case this time. But it will take a lot of pressure to make prosecutors present a real case against their friends in blue.</P>

