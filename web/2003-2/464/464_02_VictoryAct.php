<P><font face="Times New Roman, Times, serif" size="5"><b>Ashcroft wants to steal more rights</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 15, 2003 | Page 2</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>ATTORNEY GENERAL John Ashcroft is getting ready for a victory lap. Ashcroft is reportedly about to embark on a 10-day, 20-state road trip to promote the Justice Department's new "Vital Interdiction of Criminal Terrorist Organizations" Act--the "Victory" Act for short.</P>
<P>The new act follows in the repressive footsteps of the USA PATRIOT Act and would further clamp down on our civil liberties. Its provisions would include restricting Arab hawala transactions--money transfers that the government claims are used to fund terrorist groups, but which also are the main way that thousands of immigrants send money to support their families in remote areas of Africa and the Middle East.</P>
<P>It would allow the government to obtain business records without a court order in terrorism-related cases and track all wireless communications (cell phones, lap tops, etc.) with a "roving" warrant. The Justice Department's definition of "Victory" also apparently includes locking people behind bars and throwing away the key: the new act would increase sentences for anyone they label a drug "kingpin" to 40 years in prison and $4 million in fines. This provision would likely be used by the Feds as a way to continue escalating U.S. intervention in countries like Colombia--where anti-government rebels are frequently denounced as being drug lords.</P>
<P>Overall, this new act is a way for the hacks in the Justice Department to try to whip up support for the same kinds of provisions they were reportedly planning on trying to implement earlier this year under a proposed PATRIOT Act II. Public outrage over those leaked proposals forced lawmakers to temporarily step back from trying to push them through.</P>
<P>Now, we have to keep the heat on Ashcroft--and make sure that his "Victory" tour winds up being nothing but an utter failure for the Justice Department's witch-hunters.</P>

