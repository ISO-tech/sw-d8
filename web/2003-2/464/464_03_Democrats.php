<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Who is labor's "lesser evil" in 2004?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 15, 2003 | Page 3</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>BACK A Democrat with a pro-labor record--or go for someone with the best chance of beating Bush? That was the debate in the AFL-CIO Executive Council following the August 5 Democratic presidential candidates' forum in Chicago.</P>
<P>The most liberal candidates--Dennis Kucinich, Rev. Al Sharpton and Carol Moseley Braun--were never serious prospects for labor's endorsement since they're dismissed by union leaders as unelectable. </P>
<P>For the Teamsters, the Machinists and the Steelworkers, the candidate of choice is Rep. Dick Gephardt (D-Mo.), the former House Minority Leader who has a reputation of voting with labor, including opposing to the North American Free Trade Agreement (NAFTA).</P>
<P>But Gephardt did vote for the creation of the World Trade Organization, which is used by the U.S. to drive NAFTA-type agreements internationally. And he's a co-founder of the Democratic Leadership Council, the pro-corporate club of conservative "New Democrats" created to distance the party from unions, African-Americans and other so-called "interest groups." He shilled for George W. Bush when the White House was demanding support for its war drive against Iraq--and only now is sounding criticisms of the invasion and occupation.</P>
<P>But the question of "electability" also dogs Gephardt, who flopped as a candidate in 1988 and 1992--and since the "Republican revolution of 1994" has failed to lead the Democrats to retake the House of Representatives. This losing record--rather than Gephardt's checkered record--is what worries labor leaders like Service Employees International Union President Andrew Stern. "We are willing to balance the ability to win with people's position on the issues," Stern told reporters following the Chicago forum. "Many of our members would prefer someone who can win, even if they don't have the best record."</P>
<P>This debate about which Democrat is electable as president obscures the real question--of whether the Democratic Party really is the party of working people that it claims to be. It's an image that dates to the 1930s, when Democratic President Franklin Roosevelt was in office during the greatest social reforms in U.S. history--including the federal legislation that established the Social Security system and allowed the right to organize in unions.</P>
<P>But these reforms weren't granted from above by Roosevelt. They were the product of titanic struggles--sit-down strikes, mass protests, union organizing drives and more. The same is true of civil rights legislation in the 1960s, when John F. Kennedy and Lyndon Johnson were in the White House.</P>
<P>Over the last 25 years, Democratic as well as Republican presidents have presided over the rollback of many of the gains from earlier eras--from Jimmy Carter's cuts in social spending in the 1970s to Bill Clinton's support for the abolition of welfare in the 1990s, as well as NAFTA. But every election, Democrats still try to get away with posing as the party of workers--or at least the lesser evil compared to the Republicans--even as they chase the same big-money corporate contributors that bankroll the GOP.</P>
<P>Nevertheless, labor is mortgaging its future by reportedly shifting $5 million from organizing funds into politics--to boost its expected spending on the 2002 elections to $45 million. The beneficiary will be a Democratic presidential candidate who won't even be required to support labor's agenda. The only real requirement is to not be George W. Bush.</P>
<P>This logic of "lesser evilism" shows why we need a third-party candidate independent of the corporate agenda--and why working people need their own political party to fight for their interests.</P>

