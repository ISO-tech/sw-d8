<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The Democrats' shift to the left?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 15, 2003 | Page 9</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>WITH EACH passing month, leaders of the Democratic Leadership Council (DLC) are sounding more like street-corner preachers who proclaim the end of world unless the heathen repent.</P>
<P>Support for "big government" and opposition to war in Iraq won't beat Bush in 2004 and "could send us Democrats back to the political wilderness for years to come," said presidential candidate Sen. Joe Lieberman (D-Conn.) recently. At its recent convention, DLC chair Sen. Evan Bayh (D-Ind.) warned the Democrats were "at risk of being taken over by the far left."</P>
<P>The cause for this sniping is the unexpected support former Vermont governor Howard Dean has received for his rhetorically aggressive attacks on Bush. The notion that Howard Dean--a scion of the Dean Witter empire and a DLC favorite as governor of Vermont--is a radical leftist is nothing short of delusional. Nevertheless, the DLC--the conservative faction of the Democratic Party--is more intent on attacking other Democrats than on concentrating its fire on Bush.</P>
<P>This has been the DLC's modus operandi since a group of Southern and Western politicians and their corporate lobbyist cronies founded it in 1985. Its announced goal was to free the Democratic Party from domination by "special interests," meaning unions, civil rights groups and women's organizations. It wanted to bring the Democrats back to the "mainstream" after their 1984 landslide presidential defeat. "The boundaries of the mainstream were defined by the DLC's donors from corporate America--ARCO, the American Petroleum Institute, Dow Chemical, Prudential Bache, Georgia Pacific, Martin Marietta and many others," wrote William Greider in <I>Who Will Tell the People? </I>in 1992.</P>
<P>The DLC was the echo inside the Democratic Party of a capitalist class that had shifted decisively to the right. While the bosses depended predominantly on the GOP to carry out their offensive against workers, they also funded the DLC to provide a farm system of conservative politicians inside the Democratic Party as well. So when the Reagan-Bush era exhausted itself in the face of the 1990-91 recession, the bosses had a reliable candidate--DLC leader Bill Clinton--ready to take over from the Democratic side.</P>
<P>Clinton-Gore faithfully carried through the bosses' and the DLC's program--free trade, welfare deform, law-and-order and deregulation. Despite all the DLC hand-wringing, most of these policies have become mainstream among most Democratic politicians.</P>
<P>Meanwhile, the Democrats have lost all the main branches of national government they held in 1992. So the DLC strategy has actually helped to push the more reliably pro-business GOP into a position to become the dominant party for a generation.</P>
<P>This doesn't alarm the DLC too much. It represents no one beyond ambitious politicians who use its infrastructure of corporate lobbyists to advance their own careers. But this possibility of GOP dominance positively frightens many Democratic voters and constituencies.</P>
<P>This internal tension between a DLC that wants to conciliate Bush and a "base" part of which "does not recognize the legitimacy of the Bush presidency" (in the words of conservative columnist Christopher Caldwell) often makes the Democrats appear like a circular firing squad. Against this, the Republican machine looks purposeful and decisive--like a real political party.</P>
<P>There's no doubt many Democratic voters are fed up with Republican Lite DLC policies that they blame for the Democrats' failures. That's why the most pro-DLC presidential candidates--Lieberman, Edwards and Graham--haven't caught fire.</P>
<P>The DLC's predictions of Armageddon are an attempt to boost their favorites as the only "electable" candidates. If the Democratic electorate rejects the DLC favorites in the primaries, one of two things will likely take place. Either the nominee will make peace with the DLC as part of a "move to the center." Or DLC figures will continue to trash the "too-liberal" candidate, and may even launch a "Democrats for Bush" formation.</P>
<P>Either way, the corporate agenda is served. And when it comes to promoting its agenda, Corporate America is decidedly bipartisan.</P>

