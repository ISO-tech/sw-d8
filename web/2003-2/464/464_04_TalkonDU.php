<P><font face="Times New Roman, Times, serif" size="4">Pentagon's depleted uranium weapons take a toll</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Exposing their war lies</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 15, 2003 | Page 4</font></p>

<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</B></I> </P>
<P>Seven members of the International Socialist Organization went to the local Veterans of Foreign Wars (VFW) hall in Rochester, N.Y., in July to have conversations and sell <I>Socialist Worker</I> at a talk on depleted uranium (DU) weapons given by Doug Rokke, formerly a major in the U.S. Army.</P>
<P>Rokke delivered horrifying accounts of the effects of DU and the government's denial of these very dangers. From water treatment plants blown to pieces and poisoned for eternity, to tanks splattered with human remains in a debris field that won't be safe to visit for thousands of years, it was clear what the real purpose of DU weapons is. When asked, "Why would the government lie to us about DU?" Rokke responded, "Simply so they can continue using them to kill and destroy."</P>
<P>The discussion was marked by personal accounts of sickness and disgust with the realities of DU; however, the hallmark of the event was the issue of class. It wasn't Cheney, Bush, Gen. Norman Schwarzkopf or Gen. Tommy Franks who walked among the splattered bodies and obliterated hospitals. It was Barbara Andres, a member of Rokke's former team, whose parents were in the audience; it was Roberto Resto, a Vietnam vet and member of the ISO, exposed to the likes of Agent Orange; it was ordinary working people, and the audience couldn't agree more.</P>
<P>The hatred of Bush and Clinton, and Halliburton and Bechtel was palpable. For any activists, liberals, conservatives or radicals who claim that socialist politics don't resonate with the public, I'll send a copy of this discussion.  </P>
<P>It is a mistake to think that veterans and soldiers are swallowing the crap that Bush and his cronies are shoveling. Working people know what is happening to their lives and they aren't afraid to say something. They are looking for answers. They are looking for a fight. </P>
<P>You can find them anywhere, even in the VFW in Rochester. </P>
<B><P>Brian L., </B>Rochester, N.Y.</P>

