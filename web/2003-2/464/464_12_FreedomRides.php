<P><font face="Times New Roman, Times, serif" size="4">The new Freedom Rides</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>We'll fight for immigrant rights</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Orlando Sep&uacute;lveda</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 15, 2003 | Page 12</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>HUNDREDS OF national and local labor organizations, along with religious, community, and immigrant rights groups, will join the Immigrant Workers Freedom Rides beginning next month. Modeled on the example of the civil rights movement's Freedom Rides in the 1960s, the initiative is designed to highlight the struggle of immigrant workers--documented or not--to work and live with dignity in the U.S.</P>
<P>The two-week bus trip begins September 20 and will culminate in an October 1 demonstration in Washington, D.C.--and then a rally in New York City three days later, which organizers hope will draw tens of thousands of workers.</P>
<P>One goal of the Freedom Rides is to pressure the White House and Congress for legislation to legalize undocumented workers and protect their rights and safety in the workplace. But local rallies and actions to build for the Freedom Rides are taking up other issues.</P>
<P>At a July demonstration in St. Paul, Minn., activists called for restrictions on the enforcement of immigration laws. Rallies organized along the West Coast took up the death of undocumented workers trying to cross the border from Mexico--the result of tighter restrictions that have pushed immigrants to use more dangerous routes to enter the country.</P>
<P>Last weekend, more than 1,000 people turned out for an exciting indoor rally in Chicago to kick off the campaign to bring out activists for the national events next month. Speakers at the rally showed their concern for the rights of immigrant students to continue their education in colleges and universities--and for undocumented workers to obtain valid driver licenses.</P>
<P>One important show of local solidarity was the presence of striking workers at the Congress Hotel, down the street from the rally site. Henry Miller, a bartender for 25 years and member of Hotel Employees and Restaurant Employees Local 1, called on the audience to "show [the bosses] that this really is a union town." The rally ended with many participants joining Congress strikers in a noisy picket of the hotel.</P>
<P>As in a rally in New Haven, Conn., last month--where Muslim and Arab students at Yale University have been arrested and deported for minor immigration technicalities, such as a mistake in writing down a date on a registration paper--the post-September 11 witch-hunt of immigrants was on the mind of activists in Chicago.</P>
<P>"Families have been turned apart," said Kareem Irfan, president of the Council of Islamic Organizations. "Small businessmen, airport workers and taxi drivers have been deported on technicalities. Are you going keep allowing that?" A loud "No!" was the answer from the audience.</P>
<P>Everyone who cares about immigrant rights--and about pushing back against the anti-immigrant bigots' wider right-wing agenda--should build for the widest possible mobilization for the Immigrant Workers Freedom Rides.</P>

