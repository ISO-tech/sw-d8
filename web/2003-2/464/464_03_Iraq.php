<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Bush's imperial arrogance is stoking anger at home and abroad</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The iron fist of occupation</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 15, 2003 | Page 3</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>"ALL AMERICANS can be proud of what our military and provisional authorities have achieved in Iraq," George W. Bush declared last weekend in a radio address that marked 100 days since he declared the end of the U.S. war on Iraq.</P>
<P>Proud? Proud of the wreckage of a country, where the entire population has been reduced to depending on food aid to survive? Proud of an invasion justified with frantic claims about Iraq's weapons of mass destruction that turned out to be a complete fraud? Proud of an iron-fisted military occupation that regularly takes more innocent lives?</P>
<P>Like Adel abd al-Kerim and his three children. </P>
<P>It was an hour before the 11 p.m. curfew on August 8, his wife Anwar remembers, when Adel had driven his car up to a roadblock in Baghdad--slowly and carefully, so as not to upset the U.S. soldiers on guard. Then, gunfire broke out up the street. Troops at a different roadblock had opened fire at a car they thought--mistakenly--was carrying armed attackers.</P>
<P>Suddenly, U.S. soldiers were shooting everywhere, indiscriminately--with no idea of what they might hit since the street was dark because of another of Baghdad's regular power outages. "We were calling out to them, 'Stop, stop, we are a family,' Anwar told a reporter from Britain's <I>Independent </I>newspaper, "but they kept on shooting."</P>
<P>Even as bullets rained around her, Anwar crawled from the car with one injured daughter. She begged people to go help her husband and other children she left behind. But even after they stopped shooting, the soldiers prevented anyone from getting Adel and his children to the hospital. They bled to death--while U.S. troops pointed their weapons at anyone who dared to try to help them.</P>
<P>Every new atrocity like this massacre has fueled the armed resistance to the U.S. occupation of Iraq. In the town of Khaldiyah, protesters infuriated that U.S. troops were meeting behind closed doors with their local stooges ransacked the mayor's office and drove the Americans out of town. "The protest was not because we worked with Saddam," said Mohammed Ibrahim. "The protest was because they shot children and bombed the shops." </P>
<P>As <I>Socialist Worker </I>went to press, residents of the city of Basra had taken over the streets in furious rioting over the lack of electricity and clean water--as well as shortages of gasoline in a country with the world's second-largest reserves of oil.</P>
<P>The Bush administration's hawks still claim that the Iraqi resistance is the work of "remnants" of Saddam Hussein's former regime. But no one really believes this. Not even Pentagon honchos like Lt. Gen. Ricardo Sanchez, the head of allied forces in Iraq, who explained his recent decision to scale back U.S. raids this way: "I started to get multiple indicators that maybe our iron-fisted approach to the conduct of ops was beginning to alienate Iraqis."</P>
<P>Meanwhile, bitterness is also growing among the U.S. soldiers that Washington's warlords have ordered to enforce their occupation. This week, representatives of the groups Military Families Speak Out and Veterans for Peace, among others, will hold a press conference in Washington, D.C., to launch Bring Them Home Now--a "campaign aimed at ending the U.S. occupation of Iraq and returning troops to their home bases," the groups say.</P>
<P>The launching of this campaign is a sign of how advanced the frustration has become among not only soldiers' families, but active-duty troops--who are communicating their grievances via the Internet. "You call Donald Rumsfeld and tell him our sorry asses are ready to go home," wrote one private in an e-mail posted on the Traveling Soldier Web site. "Tell him to come spend a night in our building."</P>
<P>During the Vietnam War, the rebellion of U.S. soldiers--overwhelmingly working class, and disproportionately made up of minorities--was a key ingredient in Washington's defeat. But it took years to develop. The breakdown of morale among troops in Iraq is nowhere near as advanced, but the fact that it has built up so quickly shows the potential for organizing a resistance that can stop Washington's war makers.</P>
<P>The Bush gang is determined to wage its war on the world, and it doesn't plan to stop with Iraq. But this arrogant bunch is stoking anger and bitterness--both abroad and at home--that can fuel an opposition strong enough to stop their war machine.</P>

