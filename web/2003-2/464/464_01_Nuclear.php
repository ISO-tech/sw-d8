<P><font face="Times New Roman, Times, serif" size="4">White House wants "mini nukes"</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>George Bush's nuclear madness</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 15, 2003 | Page 1</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>LAST WEEK, a group of 150 Pentagon scientists, weapons contractors, civil defense officials and Strategic Command insiders gathered at the Offutt Air Force Base in Bellevue, Neb. Their mission? To build a "workable" nuclear weapon.</P>
<P>According to press reports, the discussion focused on the Bush administration plans to develop a new generation of "mini-nukes" and "bunker-buster" bombs. The maniacs at the Pentagon say that they need an arsenal of low-yield warheads to use against specific targets--to wipe out "rogue regimes" while keeping "collateral damage" to a minimum.</P>
<P>They don't care that scientists say it's impossible for such not to contaminate the environment with radioactive fallout. Or that civilian casualties would be inevitable.</P>
<P>The Bush is thinking the unthinkable. "This is a confab of Dr. Strangeloves," Daryl Kimball, head of the Arms Control Association, told Britain's <I>Guardian</I>, referring to the classic Cold War-era movie spoof about America's nuclear fanatics. "The fact that the Pentagon is barring public and congressional staff from this key meeting on U.S. nuclear weapons policy suggests that the administration seeks to discuss and deliberate on its policies largely in secret."</P>
<P>And these people dare to denounce the governments of Iran and North Korea about their supposed development of nuclear weapons!</P>
<P> "The world will not tolerate Kim Jong-il threatening international peace and security with weapons of mass destruction, particularly nuclear weapons," snarled John Bolton, a State Department bureaucrat and leading administration hawk. "While nuclear blackmail used to be the province of fictional spy movies, Kim Jong-il is forcing us to live that reality as we enter the new millennium."</P>
<P>Who is he kidding? The U.S. is already the biggest nuclear bully on the block, with by far the largest arsenal of weapons of mass destruction of any country in the world.</P>
<P>If the hawks in the Bush administration get their way, they'll actually use nuclear weapons. The U.S. government has done it before, after all. In a disgusting twist, the nuke meeting at Offutt came during the same week as the 58th anniversary of the atomic bombings of Nagasaki and Hiroshima, where hundreds of thousands of innocent Japanese civilians were incinerated.</P>
<P>We won't stand for Bush's nuclear madness. We have to build a movement against Washington's drive to war and world domination.</P>

