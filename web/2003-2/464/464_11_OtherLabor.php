<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 15, 2003 | Page 11</font></p>

<P><font face="Times New Roman, Times, serif" size="4"><b>San Francisco janitors</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By David Russitano</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>SAN FRANCISCO--Union janitors, who earlier this week were poised to strike, ratified a new five-year contract on Thursday that contains concessions.</P>
<P>The contract gives the workers a $1.40 raise over five years--only a 1.79 percent annual wage increase for janitors making the base pay of $15.65 per hour. Worse, the deal will force workers to make health care co-payments for the first time--$10 per doctor's office visit, $5 for generic drugs, $10 for brand-name prescriptions and $35 for emergency room services.</P>
<P>It was anger over demands for co-pays that led to a 95 percent vote to authorize a strike. Union leaders had been building public demonstrations and were preparing for a strike as early as August 4. Mayor Willie Brown intervened, however, and a strike was postponed. When the negotiating team finally emerged from negotiations August 8, the union had lost the momentum that was gained from the public demonstrations--and Local 1877 leaders bowed to the deal that Brown brokered.</P>
<P>Mike Garcia, the president of the union, was trying to sell this as a victory for the union. "We believe that the mayor's mediation process produced significant improvements on our major issues," he said. He also said labor, business and government "need to partner to create real solutions to this health care crisis." That's a stark contrast to the public statement he made last week: "We will strike...if they don't do the right thing."</P>
<P>This fight could have galvanized the fight around health care and would have had a huge impact on the labor movement in San Francisco. Ultimately, strikes and union actions are going to be what wins the fight around health care. We need leaders who are willing to fight the employers and government, not "partner" with them.</P>

