<P><font face="Times New Roman, Times, serif" size="4">Victims of the Cook County office building fire are...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Dead because of political clout"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Carole Ramsden</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 31, 2003 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">"PEOPLE DIED because of political clout, and they died because the politicians would spend money to buy expensive computers to reward their friends who buy tickets to upscale political dinners, but they won't put money in to make a situation safe for employees."</P>
<P>Those were the bitter words of Cook County Public Guardian Patrick Murphy after an October 17 fire that killed six people at the county's office building in downtown Chicago. The six county employees, three of them from Murphy's office, died of smoke inhalation after they were trapped inside a stairwell--because doors that should have been unlocked in a fire situation remained barred as a "security measure."</P>
<P>Grieving families of the victims were left asking numerous questions: Who gave the order to evacuate the building? Why weren't the door locks overridden? Why weren't sprinklers and other safety devices installed in the building when it was renovated in 1996? Why didn't the county's main office building file its evacuation plan with the city's high-tech emergency center?</P>
<P>Workers claimed that they heard no fire alarms, but that an announcement over the building's public address system ordered the evacuation of the 12th floor, where the fire started, followed by an announcement to evacuate the entire building. The fire department didn't order the evacuation, Chicago Fire Commissioner James Joyce said. Joyce didn't say whether the fact that the commander of the downtown fire district is his <I>brother-in-law</I> would affect his assessment of fire department performance.</P>
<P>Why the locked doors? "The doors are allowed to be locked for security purposes," Joyce explained. "[T]hat presented a problem, yes, it did, but that is within the code and within the rights of the building management." And just who is the building management? A joint venture co-owned by Elzie Higginbottom and Robert Wislow, two real estate developers who are big backers of Chicago Mayor Richard Daley and Cook County Board President John Stroger.</P>
<P>Higginbottom, a well-known African American businessman, has backed Daley against Black opponents for almost a decade. Contracts to manage major city properties flowed Higginbottom's way despite a 1996 fire that killed four residents of a housing complex that his firm managed--and despite his being cited for 36 housing code violations between 1997 and 2002, according to <I>Crain's Chicago Business.</P>
</I><P>In the first few days after the fire, Daley, Stroger and their minions tried to repeat their sick performance during the 1995 heat wave disaster, in which more than 500 people died, or following last summer's stampede at the E-2 nightclub that killed 21. They insisted that they had done everything they could--and tried to find scapegoats, or even to blame the victims.</P>
<P>Joyce--who owes his position in part to the fact that he is a member of a Daley-allied family of Democratic Party politicians--said the dead would have been safer if they had stayed in their offices. "Generally, people are safe in a building like that if they would stay in their office and wait for help," he said. "But in a panic situation, people do unusual things."</P>
<P>But the old spin may not work this time. For one thing, Cook County State's Attorney Dick Devine almost lost his life in the fire--and has vowed that he "won't sit still for a whitewash from a promised independent investigation," according to <I>Crain's.</P>
</I><P>But no one should hold their breath waiting to learn the truth from Dick Devine. If an investigation really wanted to find out why six county workers died on October 17, it would start by asking some questions of the mayor and his cronies.</P>



