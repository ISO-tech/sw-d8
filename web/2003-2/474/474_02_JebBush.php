<P><font face="Times New Roman, Times, serif" size="4">Florida governor exploits feeding tube case</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Jeb Bush's power play</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 31, 2003 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">FLORIDA'S GOV. Jeb Bush is cynically playing politics with the life of a 39-year-old Florida woman. Republican legislators pushed through a one-time-only law last week designed to go around recent court decisions and allow Bush to order doctors to re-attach a feeding tube that has kept Terri Schiavo alive for the last 13 years.</P>
<P>Schiavo suffered a heart attack in 1990 that left her severely brain damaged. Earlier in October, Schiavo's husband Michael won the latest in a series of court decisions about his request to have the feeding tube removed, based on his claim that she had once said she never wanted to be kept alive artificially.</P>
<P>Throughout the ordeal, the religious right has been frantically pressing the case of Terri Schiavo's parents that the woman should be kept alive indefinitely. Video clips of Terri Schiavo apparently smiling at her mother have flooded around the Internet, and right-wing talk radio hosts heaped abuse on her husband.</P>
<P>Anti-abortion fanatics in Operation Rescue even organized pickets outside her hospice after doctors removed the tube. Conservatives have exploited weaknesses in Michael Shiavo's case. </P>
<P>He has no concrete evidence, like a living will, that his wife once said she didn't want to be kept alive artificially, he has since had another child with his new partner, and he stands to gain monetarily from Terri Schiavo's death. But there is no question that the religious right flocked to the Schiavo case because they see an opportunity--not only around the "right to die" issue, but their wider agenda.</P>
<P>Operation Rescue founder Randall Terry says that conservatives will "use what they consider a stunning victory to pressure lawmakers everywhere to chip away at court rulings allowing abortion and banning organized prayer in schools and the posting of the Ten Commandments in public schools, among other issues," the <I>New York Times </I>reported.</P>
<P>For his part, Jeb Bush knows that he has plenty to gain by pandering to the right. He was following the example of his big brother George, who is expected this week to sign legislation outlawing the late-term abortion procedure misnamed "partial birth" abortion by the anti-abortionists.</P>
<P>As Elaine Cassel pointed out in a column on the CounterPunch Web site, Jeb Bush may claim that he wasn't "playing God" with his new law--but he was certainly playing politics.</P>


