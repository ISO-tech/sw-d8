<P><font face="Times New Roman, Times, serif" size="4">NYC teachers speak out against overcrowding</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Crisis in our schools</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 31, 2003 | Page 4 </font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</I> <BR>
</B>Severe overcrowding--the worst in years--has hit New York City public schools. Because the No Child Left Behind Act requires that students be allowed to transfer from "failing" schools (i.e., schools that have low standardized test scores), schools in Brooklyn and the Bronx are filled beyond their capacity. </P>
<P>At Columbus High School, where I teach in the Bronx, all of the 3,700 students have to go seven periods (more than six hours) without a lunch break. School begins at 7 a.m. and ends at 5:45 p.m., and every space in the building, from auto shops to libraries, is being used as a classroom. </P>
<P>Most of the bathrooms have already been converted into office and storage space. Teachers are angry and upset at the lack of space, and at Columbus High School my coworkers and I are helping turn that anger into action. </P>
<P>Forty teachers and staff, along with students, walked a lunchtime picket line in front of the school, carrying signs like "Crowded schools lead to unsafe conditions" and "Libraries should not be used for classrooms." Chants were also raised demanding "Money for schools not for war," and coworkers quickly made the connection between the $87 billion being spent on the occupation of Iraq and the worsening situation at the schools. </P>
<P>The protest was part of a citywide effort on behalf of the United Federation of Teachers (UFT) draw attention to the disastrous reorganization of the schools pushed from the top by Mayor Mike Bloomberg and Chancellor Joel Klein. </P>
<P>But the UFT is also the focus of anger from rank-and-filers. Teachers took to task one of the union vice presidents who showed up at the picket for giving away some of our rights. </P>
<P>The union leadership actually facilitated some of the overcrowding by participating in plans to open smaller schools within large Bronx high schools. Secondly, UFT President Randi Weingarten opened contract negotiations with an offer to renegotiate our work rules on a school-by-school basis around the city. </P>
<P>This would open the door to individual principals eliminating rules that protect teachers, like class-size limits and limits on how many classes we can teach in a row. Klein is now calling for an end to tenure, seniority transfer rights and is demanding merit pay--showing that if you give the Department of Education an inch, they demand a mile. </P>
<P>In response, the UFT called a rally for respect at City Hall. The more teachers who show up, the stronger the message that will be sent to the city--and to the union leadership--that teachers are angry and ready to fight.</P>

<B><P>Peter Lamphere, </B>New York City</P>


