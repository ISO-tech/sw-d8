<P><font face="Times New Roman, Times, serif" size="5"><b>Tom DeLay's redistricting scheme in Texas</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 31, 2003 | Page 4 </font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</I></B><br>
A few weeks ago, House Majority Leader Tom DeLay (R-Texas) spent three days slouching around the halls of the Texas capitol building, lobbying to make sure his redistricting plan would be passed in this third special session of the Texas legislature. It passed late on October 12.</P>
<P>The day before, an e-mail written by Joby Fortson, legislative counsel to U.S. Rep. Joe Barton (R-Texas), surfaced, revealing the racism and complete disregard for the wishes of the people of Texas in this DeLay power grab. "This is the most aggressive map I have ever seen" and "should assure that Republicans keep the House no matter the national mood," Fortson told congressional aides.</P>
<P>Fortson was euphoric about destroying the districts of two popular liberal Democrats that Republicans had been trying to remove for years. "Ha, ha, ha," appears throughout the e-mail.</P>
<P>The district of Austin's U.S. Rep. Lloyd Doggett would be split into three parts, with the one remaining to the Democrat snaking from Austin all the way down to the Texas-Mexico border. Democratic Rep. Martin Frost's district between Dallas and Fort Worth "simply disappears," joked Fortson, by shifting Black voters in Fort Worth into a Republican-dominated district, and Black voters in Dallas into a district already heavily Black.</P>
<P>Sheila Jackson Lee will keep her Houston congressional district despite the Republicans' best efforts. Ironically, George W. Bush's buddy Ken Lay&#150;of Enron infamy&#150;financed Jackson Lee's primary campaign against Democrat Craig Washington several years ago in order to get a vote for NAFTA. She must since have outlived her usefulness.</P>
<P>Bush-style "compassionate conservatism" shines through in Fortson's comments on moving Democratic Rep. Chet Edwards into a new and hostile district. "Chet loses his Killeen-Fort Hood base in exchange for conservative Johnson County," Fortson comments. "They will not like the fact he kills babies, prevents kids from praying and wants to take their guns. State Rep. Arlene Wohlgemuth, come on down, you are the next congresswoman from Texas."</P>
<P>The new Texas redistricting plan now moves to the courts. Whatever the outcome, the redistricting shenanigans in Texas make it clear that nobody is representing the interests of the majority of the people in Texas.</P>

<B><P>Cindy Beringer, </B>Austin, Texas</P>


