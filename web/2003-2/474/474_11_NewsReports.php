<p><a name="Occupation"></a><font face="Times New Roman, Times, serif" size="4"><b>No to the occupation of Iraq</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">IN THE weeks leading up to the national mobilization on October 25, student antiwar activists built for the protests by hosting stops on the Campus Antiwar Network's "Speaking Truth to Empire" national tour. The tour had stops across the country, featuring opponents of U.S. militarism such as Howard Zinn, Noam Chomsky and Rania Masri.</P>
<P>In Greensboro, N.C., an October 9 CAN tour stop at the University of North Carolina-Greensboro drew 70. The audience represented a core of antiwar activists from the around the state. In a follow-up meeting, campus activists planned for an "Arts Against the Occupation" festival event.</P>
<P>In Boston, five speakers came to Northeastern University October 21 to address an audience of 75, one of the largest meetings ever of the campus antiwar coalition. Dr. Karim Khudairi, a native of Iraq and professor emeritus at Northeastern, and longtime anti-sanctions activist George Capaccio talked about the impact on the Iraqi people. "Iraq has been occupied now by four empires in its history," Khudairi said. "History is being repeated in the 21st century."</P>
<P>In Burlington, Vt., the CAN tour came to the Memorial Auditorium on October 22, packing 200 into an overflowing room. One speaker who recently returned from Iraq shared the opinion of a cab driver he talked to--that the U.S. "wants us to be afraid like we were afraid under Saddam." Antiwar activist Rania Masri, meanwhile, linked the war abroad with the war at home, describing the poverty draft that fills up the ranks of the U.S. military.</P>

<I><P>Pete Ivey, Ben Larrivee and Mary Howland contributed to this report. </P>
