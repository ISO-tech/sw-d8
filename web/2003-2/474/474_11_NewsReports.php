<p><a name="Liberty"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend civil liberties</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Pete Petraro</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Some 150 people turned out to protest last week at Columbia University against the USA PATRIOT Act. The rally was sponsored by a local chapter of the American Civil Liberties Union and included speakers from political offices and various activist organizations. </P>
<P>Some of the protesters carried signs that connected the injustices of the U.S. occupation of Iraq with the stifling of opposition at home. Students said that they turned out because of the threat to their rights--and to the families who face racist scapegoating promoted by the civil liberties-shredding law.</P>



