<P><p><font face="Times New Roman, Times, serif" size="4"><b>Defend the Berkeley 3</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Sid Patel</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">BERKELEY, Calif.--Three University of California-Berkeley (UC) students will face a sentencing hearing on October 28 as <I>Socialist Worker </I>went to press. Their "crime"? Playing a leading role in a campus antiwar protest on the day that George Bush's war on Iraq was launched.</P>
<P>UC administrators have steamrolled the three activists in a process riddled with violations of the students' rights. Now they face punishments that could make it practically impossible for them to participate in campus protests again.</P>
<P>None of the sit-ins at UC-Berkeley over the last decade have been punished this way. But administrators want to set a precedent. That's why they have decided to hold the hearing far away from central campus, in a clear attempt to hide what is taking place.</P>
<P>But activists on campus have a different idea. They have launched a broad appeal for support through an online petition and an open letter in defense of the Berkeley 3. </P>
<P>So far, the response has been huge--people across the country now know about the administration's attack on the right to dissent in the historic home of the Free Speech Movement of the 1960s. The online petition--which demands that the charges against the students be dropped--has accumulated more than 1,500 signatures, coming from scores of campuses, as well as several countries internationally.</P>
<P>This outpouring of support makes it clear that people from all walks of life--even a retired Air Force colonel--are shocked and disgusted by UC's actions. In addition, hundreds of people have signed an open letter to administrators demanding a new and fair trial. </P>
<P>Union activists--including several teachers' unions, the International Longshore and Warehouse Union and several campus unions--have signed onto the letter, as well as leaders of progressive organizations and publications such as Global Exchange and <I>War Times </I>newspaper.  The letter appeared as a full-page ad in the campus newspaper the day before the hearing--and encouraged people to attend the protest right before the sentencing hearing. Activists hope that a good turnout will put pressure on the hearing committee to back down and respect students' rights.</P>

