<P><font face="Times New Roman, Times, serif" size="4">Wal-Mart immigration raids</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Feds' racist scapegoating</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 31, 2003 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">EVERY NIGHT for 16 months, Victor Zavala Jr. showed up for work to clean floors at the Wal-Mart store in Old Bridge, N.J. That's where he was on October 23--when he was arrested during a raid by federal immigration officials on Wal-Mart stores in 21 states.</P>
<P>By the time the night was over, more than 250 undocumented immigrants--mostly cleaners employed by Wal-Mart contractors--had been arrested. Now Victor, his wife Eunice and their three children face deportation to Mexico.</P>
<P>Incredibly, the Feds had the gall to claim that the raid was staged to "protect" undocumented workers. "We're just trying to ensure that employers remain compliant with the law, that they're not undercutting or paying the illegals two bucks a day, to make sure they're not exploiting alien workers," Garrison Courtney, a spokesman for the Bureau of Immigration and Customs Enforcement (ICE) told the <I>Washington Post</I>.</P>
<P>But the truth is that the raids are a disgusting display of racist scapegoating by the Bush administration--and a calculated move to whip up sentiment against immigrants in the wake of last month's union-sponsored Freedom Ride that drew thousands out to rallies for immigrant rights in cities across the country.</P>
<P>While the Feds focus on prosecuting and deporting workers like Victor and his wife Eunice, the real criminals are Wal-Mart and its cleaning contractors. According to the <I>New York Times</I>, in the 16 months that he worked at Wal-Mart, Victor was allowed just two days off: Christmas and New Years.</P>
<P>"We don't know nothing about days off," he told the <I>Times. </I>"We don't know nothing about nights off, we don't know health insurance, we don't know life insurance, and we don't know anything about 401(k) plans." According to the <I>Times</I>, the contractor that Victor and his wife worked for paid them just $400 a week for working 56 hours--just $6.25 an hour, including overtime for all the extra hours the couple worked.</P>
<P>But don't expect any Wal-Mart executives to be arrested. If the company is found to have known about the workers' illegal status, the most it will face is a fine of $10,000 per worker, a slap on the wrist for one of the most profitable--and anti-union--companies in the U.S.</P>
<P>"The best protection that could be given to undocumented workers would be to fix the broken immigration system," says David Koff, communications director for the Immigrant Workers Freedom Ride. "That means enabling them to become legal in this country and to pursue citizenship if they wish, to have the rights of workers who are citizens or documented immigrants, and to be protected in their workplace and have the ability to organize unions to defend themselves, without fear of deportation or other forms of punitive action."</P>


