<P><font face="Times New Roman, Times, serif" size="4">From abortion ban to anti-immigrant raids at Wal-Mart</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bush's right-wing rampage</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 31, 2003 | Page 1 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON'S RIGHT-WINGERS are on the rampage--and they want to steamroll over our rights.</P>
<P>The anti-abortion bigots were celebrating their victory over women's right to choose abortion last week as the Senate approved legislation banning a late-term procedure. Seventeen Democrats joined Republicans to vote for a ban on a procedure that abortion opponents have misnamed "partial birth."</P>
<P>The conservative Family Research Council declared it "a monumental day for the sanctity of human life." But the life of a woman doesn't seem to count. The ban on the procedure--which is used exclusively when a woman's health is in danger or the fetus is severely deformed--doesn't contain an exception for the health of the woman. </P>
<P>As <I>Socialist Worker </I>went to press, the bill was headed to George W. Bush--and there's little question that he will sign it into law. In several states where similar "partial birth" bans were passed, the laws were so vaguely worded that abortion providers in some cases stopped performing abortions at all--because they were afraid of being put in jail.</P>
<P>Women's rights aren't the only thing on the White House hit list. Last week, federal agents seized some 250 undocumented immigrants who working for contractors that clean 61 Wal-Mart stores around the country.</P>
<P>If fined, Wal-Mart would have to pay as little as $10,000 per worker--a pittance for one of the most profitable companies in the U.S. But the immigrant workers will be deported.</P>
<P>Meanwhile, Washington showered Corporate America with more gifts when the Senate Finance Committee approved a bill that would give a tax holiday to corporations with $400 billion in foreign profits.</P>
<P>For most people who want to stop this right-wing free-for-fall, the pressure will be on to get Bush out of office in 2004. They should remember the record of the last Democrat in the White House.</P>
<P>Bill Clinton may have vetoed past congressional attempts to push through federal "partial birth" bans, but he also gave up ground to the right on the abortion issue--abandoning his promises to push for a Freedom of Choice Act, for example. Clinton's lukewarm support allowed the anti-abortionists to go on the offensive, chipping away at the right to choose.</P>
<P>It's time to remember that the <I>Roe v. Wade </I>decision that legalized abortion came from a Supreme Court stacked with justices appointed by Richard Nixon--because tens of thousands of activists organized to force change from Washington.</P>
<P>We can't wait to see what happens after the election. The time to start organizing against Bush's right-wing rampage is now!</P>



