<P><font face="Times New Roman, Times, serif" size="5"><b>Turning prisons into mental institutions</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 31, 2003 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE U.S. "justice" system is warehousing the mentally ill not in mental health facilities--but American jails and prisons. That's the grim conclusion of a Human Rights Watch study released last week.</P>
<P>As many as one in five of the 2.1 million people in U.S. jails and prisons suffer from serious illnesses, the study found. Meanwhile, budget cuts have cut the number of beds in mental health facilities, leaving fewer than 80,000 people in state mental hospitals. In other words, Human Rights Watch researchers concluded, U.S. prisons have become the country's "default mental health system."</P>
<P>"I think elected officials have been all too willing to let the incarcerated population grow by leaps and bounds without paying much attention to who in fact is being incarcerated," said Jamie Fellner, director of U.S. programs for Human Rights Watch.</P>
<P>Fellner told reporters that prison officials recognize the growing crisis, and Reginald Wilkinson, director of the Ohio Department of Rehabilitation and Correction, agreed. "Some peope won't like it, and the picture it paints isn't pretty," Wilkinson told the <I>New York Times. </I>"But getting these facts out there is progress."</P>



