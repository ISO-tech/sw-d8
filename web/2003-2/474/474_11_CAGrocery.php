<P><font face="Times New Roman, Times, serif" size="4">Southern California grocery strike a key battle</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"We have to show unity"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Evan Kornfeld and Gillian Russom</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 31, 2003 | Page 11 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">LOS ANGELES--Seventy thousand members the United Food and Commercial Workers (UFCW) union in Southern California have entered the third week of their strike against three big grocery chains. The workers are resisting employers' demands for drastic cuts in health and pension benefits, as well as the creation of a two-tier pay system.</P>
<P>The strike began October 11 against Vons, owned by Safeway. The Ralphs chain--owned by Kroger--and Albertsons then locked out the union. This showdown is part of a larger fight that's put some 13,000 more UFCW members on the picket line in the Midwest as well.</P>
<P>The outcome of the battle in Southern California will determine the fate of unions throughout the grocery industry. Picket lines have remained in place throughout the region, with the strikers taking six-hour shifts.</P>
<I><P>Socialist Worker</I> attended a picket-line rally at a Vons in Echo Park that was organized by Kathleen Doyle, who's worked at the store for seven years. About 35 workers and 15 community supporters turned out, including representatives of the striking Metropolitan Transportation Authority bus mechanics.</P>
<P>A spokesperson for the bus mechanics, Fernando, told the rally: "Good unions mean a good community and a good society...We have to show unity with all the unions in the county." He drew cheers when he declared, "This is the time for universal health care." Kenneth Muse, a UFCW picket captain, told <I>Socialist Worker</I> that if the grocery workers return to work first, they'll march on the mechanics' picket lines. "We're all fighting for the same cause," he said.</P>
<P>A striker named Margaret explained how her husband worked for the Los Angeles Unified School District for 35 years, but was denied health insurance coverage for his dialysis. "If I wouldn't have had my coverage, we would have been in the street," she said.</P>
<P>There have been accusations that market chains have been violating health laws by transporting milk and meat in unrefrigerated trucks. Also, <I>Axcess Business News</I> revealed that Safeway CEO Steven Burd dumped over $21,400,000 in Safeway shares in the weeks just before the strike.</P>
<P>Some Wall Street analysts believe that Safeway wants concessions from the union to pay for debts incurred by buying up supermarket chains since 1998. "They just want more gain," said Kenneth Muse, the picket captain. "They've eaten the fat, now they want to cut into the muscle."  </P>
<P>"Things really need to change in this country," added Kathleen Doyle. "We have the best health care--but only one percent of our population can take advantage of it--while we get the leftovers. They need to overhaul our government. I've always known these things but it took until the strike before it hit me that I had to do something about it." She organized the rally because, she said, "it's the small things growing into the big things that make a difference."</P>
<P>Carmen Gonzalez, Esther Barillas, and Adolfo Rivera, who work at the Ralphs at Third and Vermont in LA, joined the Echo Park rally. Inspired by the rally, the three plan to organize one at their own Ralphs in order to raise spirits on the picket line.</P>
<P>While the Teamsters union has promised to support the strike by honoring UFCW picket lines, they continue to drive delivery trucks to the grocery stores where scab drivers or managers drive them through the lines. Moreover, the promise to stop deliveries to Ralphs, Vons and Albertsons distribution centers have yet to come through.</P>
<P>One striker, who has spoken with a Teamsters member at the Vons warehouse in Santa Fe Springs told <I>Socialist Worker</I>, "The members of the Teamsters want to be out here supporting us, but the leadership is putting the brakes on. We're going to meet with some of the Teamsters to try to tell them what's going on so we can get them fired up and involved." Teamsters members need to push their union to show real solidarity with the UFCW by stopping deliveries to the grocery stores and their distribution centers until the end of the strike.</P>
<I><P>Randy Childs also contributed to this article.</I> </P>



