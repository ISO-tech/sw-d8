<p><a name="NYC"></a><font face="Times New Roman, Times, serif" size="4"><b>New York City teachers</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Megan Behrent, United Federation of Teachers</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--More than 10,000 teachers rallied at City Hall last week, protesting overcrowding in schools and new work rules instituted this year. These rules have led to micromanagement of teachers by administrators and are widely seen as hampering teachers' ability to teach their students effectively. </P>
<P>The United Federation of Teachers (UFT), the union that represents more than 80,000 educators in New York's public schools, called the "Rally for Respect"--the first major teachers' rally since the contract expired last May. Yelling chants of "Let teachers teach!" teachers expressed their immense anger at Mayor Bloomberg's meddling in every aspect of teachers' jobs--from instituting a standardized curriculum for math and English, to enforcing rigid guidelines for the timing of lessons, to dictating the exact layout of classrooms, going so far as to distribute memos about the placement of staples on bulletin boards!</P>
<P>"I'm tired of people who have never even spent a year in a classroom telling me that I'm standing in the way of them teaching my students," said one teacher from the podium. Last week's rally came at a time when teachers are under increasing attack by Mayor Bloomberg and the chancellor of schools, Joe Klein.</P>
<P>Negotiations for a new contract have been stalled since they reopened in September. The week before, Klein unleashed a new offensive--proposals calling for an end to tenure, seniority rights and separate pay scales for teachers based on shortage areas in an attempt to divide teachers and scapegoat them for overcrowded and underfunded schools. </P>
<P>"It's time to invest in the new three R's," UFT President Randi Weingarten told the rally. "Respect for teachers. Retention of qualified staff. And resources for schools." </P>
<P>While the rally was an important step in gathering teachers to express their anger, the union leadership provided no concrete strategy for organizing a real fightback capable of winning a decent contract. In fact, Weingarten herself offered concessions on work rules before contract negotiations even began. </P>
<P>It's time to start building rank-and-file power that can win real victories in work conditions, a decent contract and improve education in our schools.</P>

