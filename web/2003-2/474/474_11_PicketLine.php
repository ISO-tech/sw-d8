<p><a name="UMCP"></a><font face="Times New Roman, Times, serif" size="4"><b>University of Maryland-College Park</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Nick Chin</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">COLLEGE PARK, Md.--Two hundred people rallied in front of the administration building at the University of Maryland&#151;College Park October 22 to fight back against the latest tuition increase by the Board of Regents. Protesters demanded that the university give back the money from the tuition increases, return laid-off workers to their jobs and give workers a new contract.</P>
<P>The union has been in negotiations with the university for 22 months, four of which were spent on setting ground rules. "Power concedes nothing without a demand," said State Senator Paul Pinsky, quoting Frederick Douglass. Pinsky went on to request that the administration "expand its vocabulary" beyond the word "no" and called for them to "bargain in good faith." </P>
<P>The Board of Regents has passed yet another tuition increase, this time planning to raise in-state tuition to $7,426--an increase of 9.9 percent. "When tuition spirals out of control, students and workers are asked to share the burden," said Sally Davies, president of the AFSCME Local 1072.</P>
<P>Asked if the protests are having any effect, Simon Fitzgerald of the campus activist group Student and Workers Unite (SAWU) said that the university has scaled down its layoffs from 300 to 100 and that 20 workers have come back. By coming together, we can organize opposition to the cuts in higher education.</P>



