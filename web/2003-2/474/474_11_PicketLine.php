<P><p><font face="Times New Roman, Times, serif" size="4"><b>Chicago teachers</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Jesse Sharkey, CTU delegate</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--In the aftermath of an unprecedented "no" vote on a contract offer endorsed by the union's bargaining team, the Chicago Teachers Union (CTU) leadership is showing signs of repairing divisions inside the union and preparing for a strike. Many delegates went into last week frustrated by the disappointing contract offer and feeling as though leadership showed they couldn't hear what the rank and file was saying.</P>
<P>But as the week progressed, it turns out that leadership was in more of a mood to listen. On Monday, officers reached out to school-level leaders, organizing "conversation on the contract" meetings with delegates to discuss our demands and next steps.</P>
<P>At the high school meeting, dozens of rank-and-file leaders spoke out in a meeting that lasted three-and-a-half hours. Delegates spoke about pay and health benefits, but also demanded a contract of no more than three years, smaller class sizes and strengthening workplace rights protected by a strong grievance procedure.</P>
<P>The meeting reinforced the powerful sense of anger coming from the rank and file. Several delegates denounced how powerless they felt in the face of the administration's harassment and disciplinary actions as retribution for exercising union rights such as filing grievances. Other delegates railed against the hypocrisy of the school board, demanding that teachers improve student test scores while refusing to lessen overcrowding in elementary classes.</P>
<P>Many delegates voiced criticisms with the way the leadership has handled the contract campaign up to now, but sentiment in the room was also overwhelmingly in favor of moving forward in a united fight against the board's proposals. Union leaders also reported that they have formed a strike committee and started production of buttons and contract campaign literature.</P>
<P>Elementary school delegates are to meet followed by a meeting of the entire house on October 29. Leadership is likely to ask for a strike vote on Wednesday.</P>

