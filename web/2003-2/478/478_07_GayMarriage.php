<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Behind the furor over gay marriage</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jeff Bale</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 5, 2003 | Page 7 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE MASSACHUSETTS Supreme Judicial Court stoked the national debate around gay marriage November 18 when it declared that it was unconstitutional for the state to exclude same-sex couples from civil marriage. The 4-3 ruling marks a significant victory for lesbians and gays in the wake of a series of recent gains and backlashes. </P>
<P>Last June, the U.S. Supreme Court finally struck down anti-sodomy laws in the 13 states that still had them. But no sooner had the decision been announced than the religious right countered with an anti-gay campaign, focused especially around the issue of gay marriage.</P>
<P>The polarization around the issue can be seen from the fact that, while several states are moving in the direction of Massachusetts and Vermont (which legalized gay civil unions in 1999), no fewer than 37 states, and the federal government, have passed laws banning same-sex marriage.</P>
<P>The marriage issue has caused controversy among lesbians and gays as well. Long considered a fringe concern of conservative gays trying to "pass" in straight society, same-sex marriage is now the dominant issue defining mainstream gay politics. </P>
<P>Part of this development is due to dramatic changes in the lives of lesbians and gays. The AIDS crisis drove home just how few rights that same-sex partners had--to make medical and legal decisions, for example. Meanwhile, the growing reality of same-sex couples adopting and having children makes questions of guardianship and family health care acutely important.</P>
<P>Still, same-sex marriage is a hot-button issue among gays and lesbians because the movement itself has shifted so far to the right. The majority ruling in the Massachusetts case declared that marriage is "a vital social institution" that all people should be entitled to. </P>
<P>Of course, all people <I>should </I>be entitled to marry, if they choose to. But the eagerness of mainstream gay rights organizations to embrace this "vital social institution"--one that fails more than 50 percent of the time for straight couples and that is a significant source of violence against women and children in society--shows how far they have drifted from predecessor groups like the Gay Liberation Front that fought in the 1960s and '70s for a revolutionary restructuring of society.</P>
<P>The gay movement's shift to the right on issues like marriage is mirrored by its obsession with supporting Democrats for political office--no matter what their actual record. And true to form, the Democrats disgraced themselves once again around same-sex marriage.</P>
<P>In the wake of the Massachusetts ruling, Senate Minority Leader Tom Daschle (D-S.D.) criticized the decision and reaffirmed his support for the Defense of Marriage Act. His comments weren't really that different from George Bush's--or bigots like religious right leader Gary Bauer, who complains that the institution of marriage is "under attack."</P>
<P>Among the candidates for the Democratic presidential nomination, Carol Mosley Braun and Dennis Kucinich did support the decision. But the main contenders had little to offer.</P>
<P>Frontrunner Howard Dean has gained a lot of support among gays and lesbians because he signed the gay civil union law in Vermont in 1999--though he did so in the dead of night. But when the Massachusetts decision came out, he ducked the question entirely by denouncing the cynical use of the issue in an election year. </P>
<P>Gen. Wesley Clark and Sen. John Edwards took the latter-day segregationist approach and declared gay marriage to be a states' rights issue. Dick Gephardt, John Kerry and Joe Lieberman adopted the mainstream Democratic message--stating their opposition to gay marriage, while vowing to oppose a constitutional amendment banning gay marriage.</P>
<P>No wonder there is so much confusion around the issue of gay marriage and the fight for gay rights in general. The ruling in Massachusetts should be celebrated as a real and concrete victory for lesbians and gays everywhere.But the fact remains that a very different kind of fight is urgently needed to put an end to gay oppression once and for all.</P>


