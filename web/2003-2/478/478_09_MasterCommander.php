<P><font face="Times New Roman, Times, serif" size="4">Historical epic film<I> Master and Commander</I></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>An ode to empire</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Joe Allen</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 5, 2003 | Page 9 </font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2"><B><I>Master and Commander, </B></I>directed by Peter Weir, written by Weir and John Collee, starring Russell Crowe and Paul Bettany.</font></P>

<P><font face="Times New Roman, Times, serif" size="3"><I>MASTER AND Commander: The Far Side of the World </I>is one of the most popular films in theaters across the country. It has received overwhelming praise from critics for its attention to historical detail, sweeping action and great acting. The film has also been embraced by many right-wing columnists, most notably the <I>Washington Post's </I>Charles Krauthammer, for promoting such "morally serious" and "war-clarifying" notions as duty, honor and Franco-phobia.</P>
<P>There's no doubt that <I>Master and Commander </I>is an entertaining film. Russell Crowe stars as British captain Jack Aubrey and Paul Bettany as his friend and alter ego, ship's surgeon Stephen Maturin. The film was directed and co-written Peter Weir, which among his many directing credits is the great antiwar film <I>Gallipoli.</I></P>
<P>Based on the novels of British writer Patrick O'Brian and set during the Napoleonic wars in 1805, almost all of the film takes place at sea. The drama of the film is between Aubrey's <I>Surprise </I>and the much larger and better-gunned French warship <I>Acheron.</I></P>
<P>We never learn the name the name of the French captain, who remains a dark, ominous figure we see only through Aubrey's telescope. After a surprise attack on the <I>Surprise </I>by the <I>Acheron </I>off the coast of Brazil, Aubrey spends the rest of the film chasing the <I>Acheron </I>with the goal of preventing it from helping Napoleon establish an empire in the Pacific.</P>
<I><P>Master and Commander--</I>both the novel and the film--are extremely conservative in politics and social attitudes, upholding as virtues the worst aspects of the British Navy and Empire. The British fleet and its captains are cast as the thin blue line between civilization, represented by the British Empire, and barbarism, represented by Napoleon Bonaparte.  </P>
<P>This comes out most clearly in the scene where Aubrey, needing to whip up the crew for their final battle with Acheron, says: "Do you want Napoleon to be your King? Do you want a guillotine to be in Piccadilly? Do you want your children growing up and singing the Marseillaise?" The crew loudly responds, "No!"</P>
<P>It's the Franco-phobia of <I>Master and Commander, </I>much in fashion among right-wing circles because of France's opposition to Bush's war with Iraq, that has Krauthammer and others bursting with bloodcurdling joy. The film "allows American audiences," according to Krauthammer, "the particular satisfaction of seeing Anglo-Saxon cannonballs puncturing the tricolor."</P>
<I><P>Master and Commander </I>is a movie you love to hate. There are many great scenes followed by a line or two of pro-British dialogue that makes you grind your teeth. Hopefully, when it is released to DVD it will have an alternative ending--a common feature these days.</P>
<P>In that ending, the British lose the Napoleonic wars, while there are successful rebellions in Ireland, India, Canada and Australia. There will be no carve-up of Africa or opium wars in China. Humanity will be saved from 150 years of the British gulag.</P>
<P>Go see the movie and root for the French!</P>

