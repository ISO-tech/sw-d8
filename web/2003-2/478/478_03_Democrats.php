<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Giving Bush the margin of victory on Medicate "deform"</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why did the Democrats cave again?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 5, 2003 | Page 3 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">VOTES IN Congress last week over Medicare and energy policy showed which party, Republican or Democrat, spouts rhetoric about helping ordinary people, but ultimately comes down on the side of corporate interests. Both of the above.</P>
<P>It's easy to see why Republicans supported the disastrous Medicare "reform" legislation that the Senate passed last week, opening the way for George W. Bush to sign it into law. The bill's prescription drug benefit is a boon for drug companies--and will actually cost many seniors much more in out-of-pocket expenses. Plus, Republican leaders added on other measures that are designed to benefit the HMO giants and undermine Medicare altogether.</P>
<P>But it was Democrats who provided the margin of victory when push came to shove last week--and not just Republicans-by-any-other-name like Sen. Zell Miller (D-Ga.), but mainstream party leaders like Sen. Diane Feinstein (D-Calif.). Likewise, Bush's energy legislation--concocted by Dick Cheney's secret White House task force--is an outrageous giveaway to Corporate America, from the $24 billion in tax breaks for coal, oil, gas and nuclear companies, to the relaxation of pollution regulations. </P>
<P>Yet the Democrats were still ready to give Bush a victory on this plank of his right-wing agenda. If not for a handful of Republican senators from New England outraged by one particular giveaway to the Texas oil boys, a filibuster to block the legislation would have failed--and the bill would have gone on to become law.</P>
<P>Why were Democrats like Senate Minority Leader Tom Daschle (D-S.D.) and even liberal Sen. Tom Harkin (D-Iowa) ready to go along with Dick Cheney's helping hand to his energy industry pals? Because the bill includes a massive increase in government subsidies for the production of the corn-based gasoline additive ethanol--which is produced mainly in...you guessed it, the Midwestern farm states that Daschle and Harkin represent. In fact, almost none of the government's ethanol subsidies go to farmers. They line the pockets of agribusiness giants like Archer Daniels Midland, which spend big money lobbying Republicans and Democrats alike.</P>
<P>On the Medicare vote, all it took was some cover provided by the American Association of Retired Persons--whose CEO Bill Novelli, an admirer of Newt Gingrich, delivered an endorsement of the legislation--and Senate Democrats began peeling off. Sen. Ted Kennedy (D-Mass.) led the half-hearted effort to block the bill, calling it "a calculated program to unravel Medicare, to privatize it, to voucherize it, and to force senior citizens into the cold arms of HMOs."</P>
<P>All true. But Kennedy bears responsibility for setting the stage. Earlier this year, he met with the Bush White House to negotiate a deal on the Medicare prescription drug benefit--and praised the initial version of the bill as "the greatest action in a generation to mend the broken promise of Medicare."</P>
<P>"He basically credentialed the Republican effort," said political analyst Stuart Rothenberg. "By the time he jumped off the train, it had already built up momentum."</P>
<P>All this was happening as George W. Bush's approval ratings have plummeted from their postwar highs. One recent poll found that 54 percent of people think he's "untrustworthy." So how did the Bush administration score another victory over the Democrats--and on the issue of health care, where millions of people are instinctively and justly suspicious of the Republican agenda?</P>
<P>This isn't a matter of Democrats being outmaneuvered by wily Republicans. The real problem for the Democrats is that, as a political party, they live a lie.</P>
<P>They regularly say one thing to win votes from their liberal base. But once in office, they do another--to serve the big business interests that really pull the strings in the Democratic Party and the political system as a whole.</P>
<P>This is something that every person who is considering voting for the Democratic presidential candidate--whoever that is--to defeat George W. Bush in 2004 should think about. The rhetoric of the contenders as they seek your support can sound good. But talk is cheap. </P>
<P>Do you want to vote for a candidate who represents a political party that is overwhelmingly funded by the same corporate cash as the Republicans? A party that serves big business as its top priority? A party whose differences with the Republicans are much smaller than what they share? We need a real alternative to the two-party duopoly that runs Washington.</P>



