<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Real target of the FBI's new powers</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 5, 2003 | Page 3 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">A PRESIDENT facing re-election and presiding over an increasingly unpopular war calculates that the best way to get back into the White House is to portray protesters as extremists, use the FBI to spy on activists and play to a conservative "silent majority." That was Richard Nixon's strategy in 1972 during the Vietnam War--and it's the model for George W. Bush today.</P>
<P>After months of a White House campaign to expand the already draconian USA PATRIOT Act, Congress gave the political police another set of tools. Legislation awaiting Bush's signature gives the FBI new powers to demand business records and financial information without the approval of a judge. Car dealers, pawnbrokers, travel agents and even gambling casinos will have to hand over information about your finances any time the FBI demands it.</P>
<P>The justification for all this? Fighting terrorism, of course. But the real target of these new police-state powers isn't al-Qaeda, but anyone who actively organizes against Bush's imperial power grab in the Middle East.</P>
<P>If this sounds like a conspiracy theory, just look at the November 23 <I>New York Times.</I> "The Federal Bureau of Investigation has collected extensive information on the tactics, training and organization of antiwar demonstrators and has advised local law enforcement officials to report any suspicious activity at protests to its counterterrorism squads, according to interviews and a confidential bureau memorandum," the <I>Times </I>reported.</P>
<P>The FBI charges that antiwar organizations have used "training camps" to organize demonstrations, and wants closer "monitoring" of such activities. This is nothing less than a revival of the counterintelligence program (known as COINTELPRO), in which FBI Director J. Edgar Hoover ordered agents to spy on Martin Luther King and tried to wreck groups like the Black Panther Party and other left-wing organizations.</P>
<P>This time, the <I>Times </I>reports, the FBI will only focus on "extremist elements" who plot violence, not "the political speech of law-abiding protesters." Tell that to the 25,000 union members and activists protesting last month's Free Trade Area of the Americas summit in Miami.</P>
<P>Thousands of police from local, state and federal agencies joined forces to carry out a military operation aimed at global justice demonstrators, union members, retirees and anyone who happened to get in their way. This is just the kind of multi-agency cooperation that the FBI is aiming at--and is doubtless seen in Washington as a model for the future.</P>
<P>But whereas Nixon used "law and order" to appeal to more conservative unionists, such as "hard-hat" construction workers, Bush's law enforcement plan is to hit those same workers over the head if they try to stand up against him. The AFL-CIO joined with the American Civil Liberties Union and environmental groups to denounce the police crackdown in Miami.</P>
<P>"The fundamental rights of thousands of Americans were blatantly violated, sometimes violently, by the Miami police, who systematically repressed our constitutional right to free assembly with massive force, riot gear and armaments," United Steelworkers of America President Leo Gerard wrote in a letter to congressional leaders.</P>
<P>The FBI's aggressive new spying will mean more such confrontations in the future. It's time to step up the effort to defend our civil liberties and our right to protest.</P>



