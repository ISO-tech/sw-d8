<p><a name="Ryan"></a><font face="Times New Roman, Times, serif" size="4"><b>Justice for Ryan Matthews</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Stephanie Schwartz</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--The New York chapter of the Campaign to End the Death Penalty (CEDP) held a Day of Justice for Ryan Matthews on November 18, gathering signatures and showing films to raise awareness of his case.</P>
<P>The injustice suffered by Ryan Matthews embodies all of the problems with the death penalty system. Ryan is a mentally retarded, Black man who was only 17 at the time he was convicted of killing a grocery store owner in Louisiana. Now, there's DNA evidence that points to the true killer.</P>
<P>As part of the Day of Justice, CEDP activists tabled at Columbia University and City College to compile signatures and help launch an ad campaign to publicize Ryan's case in New York and New Orleans newspapers. About 70 people attended the CEDP's film screenings of <I>The Road to Justice</I> about Ryan's case and <I>The Farm</I> about Angola prison, where Ryan is behind bars.</P>
<P>Emily and Sarah Kunstler, co-producers of <I>The Road to Justice,</I> spoke about the need to get the word out about Ryan's case. Liz Garbus, director of <I>The Farm, </I>spoke about the importance of using film to promote social justice.</P>



