<p><a name="SOA"></a><font face="Times New Roman, Times, serif" size="4"><b>Shut down the School of Assassins</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">FORT BENNING, Ga.--More than 10,000 activists turned out November 22-23 for the annual protest at the camp where the U.S. army trains Latin American dictators. For the last 14 years, thousands have gathered at the gates of the School of the America (SOA)--recently renamed the Western Hemisphere Institute for Security Cooperation--to take part in peaceful civil disobedience organized by the human rights group SOA Watch.</P>
<P>Every year, protesters are arrested as they attempt to "cross the line" into the camp. But in recent years, the government has used new powers granted it by the Bush administration's "war on terrorism" to terrorize protesters.</P>
<P>Activists face ever harsher prison sentences. And this year, authorities barricaded the protest area along Fort Benning Road and searched protesters--the majority of whom are members of religious groups--with metal detectors.</P>
<P>And at the gates of Fort Benning, activists taking part in a peaceful, permitted protest were blasted with anthems and martial music played from loudspeakers at high decibels. SOA Watch plans to sue Fort Benning for violating the civil rights of demonstrators.</P>
<P>"Prison will not deter us," said Kathy Kelly of Voice in the Wilderness, who was among those arrested at the protest. "We intend to close this school and to change the foreign policy that this school represents."</P>

