<P><p><font face="Times New Roman, Times, serif" size="4"><b>U.S. out of Iraq</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Matt Ivey, Sid Patel and Khury Petersen-Smith</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">ACTIVISTS ACROSS the country are continuing to build opposition to Washington's war on Iraq. In Winston-Salem, N.C., about 750 protested George W. Bush's November 7 $2,000-a-plate fundraiser lunch to raise money for his campaign.</P>
<P>In response, activists set up a Depression-era soup line, where protesters could line up and get a free hot meal. After the rally, protestors marched to the convention center, where they protested for about an hour, chanting "Feed the needy, not the greedy."</P>
<P>On November 17, approximately 400 people marched through downtown Rochester, N.Y., to protest a $1,000-a-plate campaign fundraising luncheon featuring Vice President Dick Cheney. Dozens of high school students skipped classes to attend the rally and march, which was sponsored by a coalition of community- and campus-based organizations. </P>
<P>Dave Dashefsky, a member of the coordinating committee of the Campus Antiwar Network, got a round of applause when he said that the U.S. should withdraw and "let the Iraqis decide how to spend $87 billion to rebuild their country." Protestors marched through downtown, gathering bystanders along the way. Paul, a Vietnam veteran, joined the march when he realized that it was against Cheney and the war. </P>
<P>"The United States doesn't get into a war unless its going to get something out of it," he said. "People are starving over here, and they're spending billions on this war and a $1,000 a plate on that lunch." </P>
<P>In Berkeley, Calif., the Stop the War Coalition has been organizing solidarity for the "Berkeley 3"--three student antiwar activists who have been punished by the University of California-Berkeley administration for protesting against the war. At a recent event with <I>Democracy Now!</I> journalist Amy Goodman attended by several hundred people, the coalition leafleted about the case and encouraged people to send e-mails and call the administration. </P>
<P>Goodman commended the three activists and asked them to stand so that they could receive a big round of applause from the audience, and emcee Lynn Savio--the widow of Free Speech Movement founder Mario Savio--asked people to support the activists. We have to stand up to UC-Berkeley's attack on political activism.</P>

