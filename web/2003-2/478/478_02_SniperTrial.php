<P><font face="Times New Roman, Times, serif" size="4">Politicians exploiting tragedy to further their careers</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Race to execute in sniper trials</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mike Stark</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 5, 2003 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">NO SOONER had a Virginia jury returned two death sentences for sniper suspect John Allen Muhammad than officials in Maryland, Washington, D.C., and Louisiana announced their intention to prosecute the 42-year-old Gulf War veteran for murder. The absurd idea of spending millions of dollars to prosecute a man who has already been twice sentenced to death demonstrates, once again, that the trials of Muhammad and juvenile suspect Lee Malvo are about politics, not justice.</P>
<P>Virginia prosecutors spared no expense in trying to convince the jury at Muhammad's trial that the defendant deserved to die. Jurors were subjected to graphic photos, a recording of a terrified 911call, and the testimony of prison guards who claimed that Muhammad had made a weapon out of a plastic utensil and attempted an escape. </P>
<P>Despite all this, the jury was initially divided over whether to impose a death sentence. One juror even asked if she could research the death penalty on the Internet during a court recess. That request was denied by the judge.</P>
<P>Not once during the trial was there any discussion of what might have led Muhammad to commit such terrible acts. Muhammad's attorneys had begun preparing evidence of their client's mental state, but during the bizarre episode in which Muhammad acted as his own attorney, he withdrew the defense request to introduce mental health testimony.</P>
<P>Had it been raised, jurors might have heard about the transformation that Muhammad was said to have undergone during his service in the first Gulf War. In 1991, Muhammad was a member of the U.S. Army's 84th Engineer Company and helped to demolish an Iraqi ammunition dump containing rockets filled with the nerve agent sarin.</P>
<P>The Pentagon later admitted that the process of blowing up the rockets may have vaporized dangerous amounts of sarin. "When he got back," Muhammad's ex-wife told the <I>Washington Post, </I>"he was a very angry man. I didn't know this man. The one I knew stayed in Saudi."</P>
<P>Montgomery County state's attorney Doug Gansler has been leading the charge to get Muhammad and Malvo extricated to his jurisdiction to stand trial. Gansler, who has never hidden his ambitions to be Maryland's attorney general, went so far as to say that he is for a Maryland trial as a "healing process of the community." </P>
<P>Maryland's new Republican Gov. Robert Ehrlich is also eager to take advantage the media attention surrounding the trial. Ehrlich, who overturned Maryland's moratorium on executions when he took office earlier this year, recently called for Malvo and Muhammad to stand trial in Maryland for "domestic terrorism." </P>
<P>As Paul Ruffa, who was wounded in one of the sniper shootings, told the Associated Press, "You really wonder how much of that is ego and how much of that is the pursuit of justice."</P>
<P>As Muhammad's jury was coming up with the death sentence, the trial of juvenile suspect Lee Malvo began. Prosecutors have played recorded statements made during a seven-hour interrogation in which no lawyer was present. </P>
<P>This "confession"--obtained as U.S. Attorney General John Ashcroft personally intervened to transfer custody of the suspects to Virginia, where the death penalty could be imposed more easily--is a key part of the effort to have Malvo sentenced to death. Prosecutors have virtually no other evidence showing that Malvo was the "triggerman" in the shootings, a requirement for the death penalty.</P>
<P>The defense has attempted to show how Malvo was brainwashed by Muhammad and wasn't responsible for his own actions. Numerous witnesses have testified that the youth showed no inclination toward violent behavior until he came under the influence of Muhammad.</P>
<P>Virginia is second only to Texas in its number of executions--and it is one of 17 states in the nation that allows for the execution of 16-year-olds. That's why execution-happy John Ashcroft wanted Malvo tried in Virginia--to make sure that he could be sent to his death.</P>



