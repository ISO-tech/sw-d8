<P><font face="Times New Roman, Times, serif" size="5"><b>Army reservist defends his rights</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 5, 2003 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">A ROCHESTER, N.Y., Army reserve officer is facing charges for sticking up for his rights. Capt. Steve McAlpin, of the 401st Civil Affairs Battalion, could face a court-martial and demotion because he questioned a legal waiver that his unit was ordered to sign.</P>
<P>The waiver allows his unit to be deployed in a war zone for the third time since January 2002. The 401st will go to Iraq in early December. McAlpin pointed out in a teleconference with a higher-up that the Army was probably violating federal law by not allowing members of the 401st a 12-month "stabilization period."</P>
<P>The brass obviously decided to get rid of a trouble-maker. McAlpin, a 25-year veteran of the military, was given a week and a half to "clear up his affairs" at his unit--before being banned from the battalion grounds. He was also charged with having a "negative attitude" and being "insubordinate."</P>
<P>A spokesperson for the 401st, Capt. Brian Earley, said that individuals are allowed to refuse to sign the waiver--but that McAlpin was "butting in" for other soldiers. It worked. McAlpin told a reporter last weekend that 17 other officers and enlisted soldiers from his unit refused to sign the waiver--and won't be shipping out to Iraq.</P>


