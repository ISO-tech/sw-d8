<P><font face="Times New Roman, Times, serif" size="5"><b>Why did Chicago teachers accept weak deal?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jesse Sharkey, CTU Delegate</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 5, 2003 | Page 10 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--By a vote of 15,289 to 12,786 the Chicago Teachers Union (CTU) approved a new contract November 18, ending the threat of a strike. The new deal--the second tentative offer brought back to the membership--offered small improvements over the previous tentative agreement, which proved enough to win narrow passage.</P>
<P>For example, in addition to the previously offered 4 percent raises, the new contract grants veteran teachers an extra two sick days a year and provides elementary teachers with an extra prep period. Union leadership touted these gains as a victory, saying in a statement, "With your strength we went back to the table and we came home victorious." But most members see the contract as a settlement intended to avoid a fight--perhaps not an outright defeat, but certainly not a "victory" either.</P>
<P>The contract delivers a blow to teachers on healthcare. Some members will pay an extra $1,600 per year to keep their current benefits.</P>
<P>Nor were the longstanding frustrations with poor working conditions and inequality with the suburbs addressed. The contract allocates $2 million--enough for just eight teachers to reduce class size.</P>
<P>Many of the "yes" votes came from members who calculated that with an unwilling leadership and a split membership, the time wasn't right to strike. Nevertheless, 46 percent of the union voted "no."</P>
<P>Those members are disappointed about how CTU President Deborah Lynch, who was elected as a reformer--handled the contract campaign. Fallout from the contract fight will dominate the union as it heads into a leadership election in May. But this puts many activists in a difficult position.</P>
<P>With the old guard poised to retake control of the union, many see Pro-Active Teachers Caucus (PACT), which  supports Lynch, as the only vehicle for steering the union in the right direction. But PACT has become a personal vehicle for Lynch. Lynch herself operates self-consciously along the model of Al Shanker--the 1960s business union leader of the American Federation of Teachers, the CTU's parent union. </P>
<P>For rank-and-file activists, the question raised by the contract campaign of 2003 must center on how to form a network of union members who can discuss strategy and act independently of the leadership when necessary. Had such a network existed, it might well have forced a rejection of the second agreement. The connections many delegates made during the past several months provides the best opportunity to form such a network in years.</P>



