<P><font face="Times New Roman, Times, serif" size="4">Chicago teacher explains opposition to new deal</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"I'm sick of settling"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 5, 2003 | Page 4 </font></P>

<P><B><font face="Times New Roman, Times, serif" size="3">ON NOVEMBER 18, Chicago Teachers Union (CTU) members voted 55 percent to 45 percent to approve a new contract. Below are excerpts of a letter that CTU delegate Jesse Sharkey distributed to fellow union members prior to the vote.</B></P>

<P><I>Open Letter to my Brothers and Sisters in the CTU at Senn High School:</I></P>
<P>Six years ago this March, I was standing in front of my class--it was my first year teaching--and I had a brain hemorrhage. I collapsed and was brought to the hospital. It turns out that what I had was called an Arterio-Venous Malformation (AVM) and half the people who get it die. I was lucky that one of the best surgeons in the country for AVMs is at Northwestern University Hospital. After surgery, I recovered and continued teaching.</P>
<P>The reason that I bring this up is to remind everyone that the things in our union contract have a very real effect on our lives. Six years ago, as a first-year teacher on the basic insurance plan, I was covered. Next year, a teacher in my same situation wouldn't be.</P>
<P>In our current contract offer, I find that if I want to keep my doctor and my hospital, I have to pay an extra $1,600 per year (about $460 for premiums, $1,200 for deductible.) You'll have to pay too if you use Loyola, University of Illinois-Chicago, Northwestern and five other hospitals. </P>
<P>I know we are being told by our union leadership to take this deal. Many agree with them. Health care costs are going up everywhere. It's true that they are. So are car stickers, property taxes, rent, electricity bills, even the CTA fare. I know the argument--"this contract is the best we can hope for; let's take it." </P>
<P>I know the argument because I've heard it before. In the late 1980s when they started freezing our salaries--we took that. In the early 1990s, when they gutted our grievance procedures and more--we took that too. In the late 1990s when they started reconstituting schools and firing teachers--we settled for that. Now there's "No Child Left Behind" with its re-certification requirements and another round of school closings.</P>
<P>I'm sick of settling. I voted for this new union leadership because I thought they were going to lead a fight--one that would take on Mayor Daley and the city powerbrokers. I wanted to see the union spell out some clear issues that were important to us: our pay inequality with the suburbs (especially for veterans), our health care costs, the difficult conditions under which we teach. </P>
<P>When the garbage workers won a strike for a 29 percent raise, I thought surely we had a golden opportunity. Instead, I think our leadership was planning on settling right from the start. If it had been up to them, we would have settled for the first offer. They told us to take it, or we would be out on strike. But we, the rank and file, had the guts to reject the first offer, and for a few short weeks, we started acting like a union. That was enough to win more.</P>
<P>But I can't help but feel that we only got enough to appease us. Our union never really raised many important issues--pay, more steps for veterans, real class size reductions, restoration of our grievance procedure. </P>
<P>Some people tell me that we got as much as we could hope to get. But I don't believe that the city of Chicago doesn't have the money to treat us and our students better--to fix the peeling paint in our rooms, to pay us what we deserve. No one--not the <I>Tribune</I>, not the board, not the union leadership has shown us a city budget where I can see that there is just no money. </P>
<P>In fact, there's plenty of money, and the city calculated to give us just enough to settle. In fact, this contract may well pass Tuesday's vote--it eked by the House of Delegates by 337 to 275.</P>
<P>But I'm voting "No," and I urge you to do the same. If there are enough of us to vote this offer down, then there are enough of us to fight. And if there aren't, than at least we staked out a clear message about what we think we deserve and what kind of union we intend to have in the future.</P>



