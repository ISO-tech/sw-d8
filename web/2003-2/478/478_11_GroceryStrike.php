<P><font face="Times New Roman, Times, serif" size="4">Teamsters honor pickets at grocery warehouses</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Grocery strike heats up</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Karl Swinehart</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 5, 2003 | Page 11 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">LOS ANGELES--The 70,000 striking and locked-out grocery store workers in Southern California got a huge boost just before Thanksgiving when the International Brotherhood of Teamsters declared that 8,000 drivers will refuse to cross the picket lines at the distribution centers. With picket lines out front and no deliveries in back, management will be under increased pressure to back off their assault on their employees' health care and their union, the United Food and Commercial Workers Union (UFCW).</P>
<P>Leading the charge for the employers is Safeway Inc., which provoked a strike at its Vons and Pavilions stores October 12. Albertsons Inc.--which locked out the union the next day--will also be hit hard by the Teamsters' show of solidarity. Kroger Co.'s Ralphs stores--whose workers are also locked out, but where the union has withdrawn pickets--will also be hammered. </P>
<P>The Teamsters' decision to take up the grocery store battle as their own--after a month of hesitation--is an impressive act of solidarity. The employers' demands would effectively eliminate employee health benefits in for store employees--and the Teamsters would be on the chopping block next.</P>
<P>Meanwhile, dozens of unions, elected officials and community groups have adopted stores, walking picket lines and organizing rallies with the strikers and bringing them food and refreshments. The days leading up to Thanksgiving saw an impressive series of solidarity demonstrations. </P>
<P>In the Orange County town of Fullerton, 1,000 trade unionists from many different unions demonstrated at Albertsons' regional offices November 22. UFCW International Secretary-Treasurer Joe Hansen said in Fullerton, "These workers are taking a stand against corporate greed. The supermarkets say this strike is over just $5 a month in premiums. That's a big, god-damned lie! I'm proud to say that my wife, my sister, and my 14-year-old son are on a picket line right now at a Safeway in Washington, D.C.!" </P>
<P>United Teachers Los Angeles had a good showing at the Fullerton rally, as did Teamsters, Hotel Employees and Restaurant Employees and the Communications Workers of America. The demonstrators in Fullerton marched from Albertons' offices to a nearby Albertsons store. When the march arrived at the store and discovered that it was "temporarily closed," hundreds of UFCW members began banging on the windows and walls of the store yelling, "Scabs go home!"</P>
<P>On the other side of the LA basin, seventy UCLA students marched two miles from campus to join the picket line at a Vons. Many labor and community groups have organized solidarity pickets outside grocery stores. Pride at Work, the gay and lesbian organization in the AFL-CIO, organized a spirited rally at a Vons in Hollywood along with other gay and lesbian groups.</P>
<P>While community support gives huge confidence to the workers, the Teamsters' decision to support the UFCW is crucial. The recent extension of picket lines to Northern California and East Coast Safeway stores also represents a real advance in this struggle. </P>
<P>Still, many in the labor movement think that picket lines should also return to Ralphs stores, too. Although the UFCW has told the public that they can support the strike and shop at Ralphs--where their members are still locked out--many other unions and labor federations are telling their members to stay away from Ralphs.</P>
<P>Addressing the demonstration in Fullerton, California Federation of Labor Executive Secretary Treasurer Art Pulaski spoke to the crowd. "I am here to speak on behalf of 2 million union members in California. We will not shop at Albertsons, we will not shop at Safeway, and we will not shop at Ralphs, until UFCW workers have their health care!" </P>
