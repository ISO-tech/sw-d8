<P><font face="Times New Roman, Times, serif" size="4">Meeting to plot theft of oil canceled, but...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Corporate vultures eye Iraq</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 5, 2003 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE CORPORATE invasion of Iraq hit a snag this month when the University of Maine's "Doing Business in Iraq: The Private Sector" conference was postponed. The $1,500-per-person conference, slated for November 13 in Scarborough, Maine, was cosponsored by the U.S.-Iraq Business Alliance, an investment group made up primarily of dozens of major Fortune 500 corporations.</P>
<P>Headed by Dennis Sokol, of the American Hospital Group--which is notorious for opening for-profit clinics in Moscow and Kiev following the fall of the ex-USSR--the conference was designed to promote corporate investment in Iraq and provide easy-access for American companies itching to get a share of the profits. As Sokol cynically told the <I>Bangor Daily News,</I> "When there are diamonds and gold out in the street, you don't wait a couple of years to go pick it up."</P>
<P>That explains why planned sessions at the conference included "Understanding the Contracting Process for the Reconstruction of Iraq"--slated to be run by a U.S. deputy undersecretary of defense--as well as sessions on infrastructure "development opportunities" and the privatization of the Iraqi oil sector.</P>
<P>But the circling of the corporate vultures prompted antiwar and global justice activists to organize against the conference, initiating letter-writing campaigns and planning protests. Earlier this month, university officials announced that the conference was canceled because two members of the U.S.-installed Iraqi Governing Council were unable to leave Iraq due to "concerns related to the current situation."</P>
<P>But activists say that it was the protests that stopped the conference. "It was the power of the people who spoke out that canceled this conference," said Veterans for Peace member Jack Bussell at a press conference celebrating the cancellation.</P>


