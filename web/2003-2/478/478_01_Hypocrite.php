<P><font face="Times New Roman, Times, serif" size="4">Bush's cynical Thanksgiving stunt in Iraq</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>What a hypocrite!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">December 5, 2003 | Page 1 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">A "PHOTO opportunity" for next year's election. That's what Jari Sheese thought of George W. Bush's Thanksgiving trip to Iraq to pose with U.S. soldiers. The man who hasn't attended a single funeral among the hundreds of troops killed in his war for oil and empire got himself smuggled into his new colonial possession last week--at an untold cost for the tightest security imaginable.</P>
<P>He hung around long enough for the cameras to catch him shoveling some turkey onto plates for the soldiers. Then it was back to the U.S. for Bush.</P>
<P>But not for soldiers like Jari Sheese's husband. "My husband has been away from home for 308 days now," Sheese said. "Today, I counted to just see how that compared to the few hours George Bush spent in Iraq, how that compared with the time he spent away from his family. </P>
<P>"In the end, it just seemed like a photo opportunity that will be used for his election next year. It makes me wonder what else he has up his sleeve...So for now, all I have is one more day that I didn't get a knock at my door by some soldiers in uniform. That is what I am thankful for this day--no thanks to George Bush."</P>
<P>The mainstream media treated Bush's photo-op-and-run visit as a masterful political move. But they had less time for the frustrated soldiers that he exploited for political gain. Or the soldiers' families who wonder when--and if--their loved ones will return. And they had no time at all for the suffering of Bush's colonial subjects--the millions of Iraqis who struggle to survive without jobs, who still lack basic services like clean water, who endure the humiliation and violence of Washington's occupation.</P>
<P>The scale of that violence was seen again this week in Samarra, where at least 54 Iraqis were killed and dozens more wounded when U.S troops began firing indiscriminately at civilian targets after reportedly being ambushed. According to the Associated Press, witnesses said U.S. tanks fired on people dropping off the injured at a hospital.</P>
<P>A local kindergarten was damaged by the tank shells. "Luckily, we evacuated the children five minutes before we came under attack," said Ibrahim Jassim, a guard at the school. "Why did they attack randomly? Why did they shoot a kindergarten with tank shells?"</P>
<P>The answer is that this is the logic of Washington's occupation of Iraq. All resistance is met with crushing force, and all Iraqis are viewed as potential enemies. This disregard for Iraqi lives--as well as for the U.S. soldiers that Washington uses as cannon fodder--will continue as long as the U.S. remains in Iraq. That's why we say: End the occupation! Bring the troops home now!</P>



