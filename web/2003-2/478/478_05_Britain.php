<P><font face="Arial, Helvetica, sans-serif" size="2"><b>BRITAIN</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A proper greeting for Bush</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 5, 2003 | Page 5 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">GEORGE W. BUSH got the welcome he deserved during his trip to Britain last month--a massive antiwar protest in the streets of London.</P>
<P>Bush's handlers had planned the visit months before, assuming that their boss would ride at the head of a victory parade. But with Iraq in chaos and the British antiwar movement regaining momentum, Bush hardly dared to show his face, canceling his speech to Parliament and the usually obligatory state procession.</P>
<P>The highlight of the three days of protests was a giant march through London. The BBC reported that the crowd--estimates ranging from 100,000 to 200,000--was the biggest weekday protest ever in London.</P>
<P>The mood was festive, the crowd a diverse collection of young and old, students, teachers, unemployed, unionists, white, Black and Asian. At the height of the protest, an immense mock statue of George Bush--with Tony Blair hiding in his pocket--was pulled down to a chorus of cheers.</P>
<P>"We want the rest of the world to basically see that a lot of British people don't support Bush," Angela, one of the protesters, told reporters. "We don't want our soldiers coming back home dead. We don't want innocent Iraqi people dying in our name."</P>
<P>The Bush administration had asked that U.S. Secret Service agents be given immunity in the event that they shot and killed any protesters, but British authorities refused to grant the request.</P>
<P>Highlighting the arrogance of her husband's administration, Laura Bush brushed off the historic turnout--the largest since 2 million people took to the streets on February 15. "We actually have not seen that many protests," said Laura Bush. "I don't think the protests are near as large as everyone was predicting before we got there."</P>



