<P><font face="Times New Roman, Times, serif" size="5"><b>Toxic Texan helps his corporate pals</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 14, 2003 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE TOXIC Texan is letting corporate polluters off the hook again. Last week, Environmental Protection Agency (EPA) officials announced that they would be dropping dozens of lawsuits against coal-fired power plants that were initiated during the Clinton administration.</P>
<P>Under the Clean Air Act, utilities used to have to install additional pollution controls when they upgraded, expanded or modernized plants to produce more electricity. But energy companies have long complained that this prevents them from making more profits.</P>
<P>So last year, their buddies in the White House decided to give them a helping hand by gutting the law and lowering the pollution "threshold" so that utilities--particularly coal-fueled power plants built prior to 1970--could expand production without worrying about the improved controls.</P>
<P>Bush administration officials had claimed the new rules would have no effect on pending cases--and that violations already in the process of being taken to court would still be "vigorously" pursued. But EPA officials suddenly announced last week that they were dropping all 50 of the pending prosecutions of coal-fired plants--some of the worst polluters in the country.</P>
<P>"It's like our worst nightmare," Frank O'Donnell, executive director of Clean Air Trust, told the <I>Los Angeles Times.</I> "They're taking the enforcement cop off the beat." In fact, environmentalists say that the Bush administration deliberately dragged its feet on prosecuting the offending companies--in order to give the EPA more time to make sure the law was changed to let polluters off the hook.</P>
<P>And it's not just clean air that's taking a beating. According to the <I>LA Times</I>, administration officials have drafted a rule that would gut the Clean Water Act, stripping pollution controls from many wetlands and possibly millions of miles of streams--and opening them up to commercial development.</P>



