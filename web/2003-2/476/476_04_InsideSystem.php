<P><font face="Times New Roman, Times, serif" size="4"><b>A wake for clean air</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">STAFFERS AT the general counsel's office at the Environmental Protection Agency had a fun Halloween party this year. It was billed as a wake for the Clean Air Act.</P>
<P>About five dozen career staff from the counsel's office and from around the building attended the event, which featured "wanted" posters of Jeffrey R. Holmstead, assistant administrator for air and radiation, sporting a mustache. </P>
<P>The posters said: "Wanted: suspected in the untimely death of C.A. Adams. [CAA is the common shorthand for the Clean Air Act] Reward: $27,500 per violation per day." Those are the penalties for violations of the CAA.<BR>
-- <B><I>Washington Post</I>, </B>November 3, 2003</P>

<p><font face="Times New Roman, Times, serif" size="4"><b>This election brought to you by... </b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">IN A move that takes corporate funding of politics out of the backrooms and puts it right up front for everyone to see, the cash-strapped South Carolina Democratic Party has come up with a new plan. Get advertisers to pay for its first-in-the-South February 3 Democratic presidential primary.</P>
<P>State party Chairman Joe Erwin, a Greenville marketing executive by trade, sees corporate logos on ballots as a way to pay the bills for the primary. He plans to use corporate sponsorships to help raise $500,000.</P>
<P>So why advertise on a primary ballot or other election materials? "Some statewide corporation may want their company identified with democracy," Erwin said.</P>
<P>About three months ago, Erwin said the party was starting from scratch on raising the money. State Democrats plan to raise some of it from soft-money donations that won't have to be disclosed, but if a corporation wants to give a little extra to slap its name on a ballot, a media backdrop--or pretty much anything--Erwin will consider it. It's a takeoff on the way ballparks sell ads on scoreboards or the backs of seats. </P>
<P>"You do what you have to do," he said, "as long as you do it legally and with integrity." Sponsorships and logos "somewhat changes the nature of politics," Erwin concluded, "but boy, isn't it consistent with the way things are changing?" <BR>
-- <B>Associated Press, </B>October 4, 2003</P>

