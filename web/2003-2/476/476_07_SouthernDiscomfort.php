<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The Democrats' Southern discomfort</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 14, 2003 | Page 7 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">HOWARD DEAN, the Democratic presidential frontrunner, stirred controversy when he said that he wanted to seek the votes of "guys with Confederate flags in their pickup trucks." Dean may not have meant his statement to be interpreted as an endorsement of the Confederate flag. But he's never explained why he's consistently used this image as an example of how he plans to broaden his appeal.</P>
<P>This is especially questionable when you consider that the standard rap on Dean coming from conservative quarters of the Democratic Party is that he won't play well among voters in the South. Coming on the heels of Democratic defeats in governor's races in Kentucky and Mississippi, Dean's comments ignited another round of punditry on whether the Democrats are finished in the South.</P>
<P>For the century after the Civil War, the Democratic Party held a stranglehold on the "Solid South." And after the Civil Rights revolution, no one but the most hard-bitten racist would openly defend Jim Crow and the denial of voting rights to Blacks.</P>
<P>But beginning with the 1968 Nixon campaign, the Republicans have succeeded in largely shifting the South from the Democratic to the Republican column. Instead of using crude race-baiting, the Republicans started to use racial code words--talking about "welfare cheats" and being "tough on crime."</P>
<P>Now the South is the heartland of the Republican Party and of conservative politics generally. As a result, every scheme that Democrats devise to win in the South always seems to be a pale imitation of the Republican formula. Since Southerner Al Gore blew the 2000 presidential election, Democratic consultants have talked about appealing to "NASCAR dads"--perhaps the more polite way of describing "guys with Confederate flags on their trucks."</P>
<P>Downplaying social issues like abortion and gay rights, playing up religion, supporting a strong military and a weak social safety net is the only way to win, the consultants say. Following the Dean controversy, the right-wing Democratic Leadership Council (DLC) proclaimed that Bill Clinton showed how the Democrats can win in the South--by being tough on crime and ending welfare.</P>
<P>All of these theories start from the assumption that the crucial "swing voter" to win is the Southern white male who drives a pickup with a gun rack and so on. Obviously, there's more to the South than this stereotyped image would admit. But the focus on this image explicitly accepts that the only way to win is to appeal to conservatives.</P>
<P>It also means ignoring Black voters--the most loyal supporters of the Democrats--almost half of whom live in the South. In 1988, the Democrats--with prodding from the DLC--created the "Super Tuesday" presidential primary in Southern states to give a leg up to conservative candidates from the South, like Gore and Clinton.</P>
<P>But in 1988, Jesse Jackson swept most of the Super Tuesday primaries with his much more liberal agenda. This should have given the Democratic poobahs an indication of who their most loyal supporters were in the South.</P>
<P>It shouldn't be hard to make the case that that Bush and the Republicans have nothing to offer to working-class Southerners--or anyone else for that matter. This, according to Dean's defenders, was what Dean meant by his Confederate flag comment.</P>
<P>But it's a message that lacks credibility when it comes from the mouths of Democratic Party politicians who, in many cases, are in hock to the same interests Bush is. Democrats are happy to have support from unions. But they're not about to jeopardize their campaign contributions from Wal-Mart to insist that the antiunion mega-chain recognize unions in its own stores.</P>
<P>They may rail at Bush's failed economic policies--which have hurt workers in the South more than other parts of the country--but they still support the same "free-trade" measures that have cost millions of jobs.</P>
<P>Growing numbers of military families--many of them in the traditionally pro-military South--are beginning to question Bush's entire Iraq adventure. But since all serious Democratic contenders--Dean included--support the continued occupation of Iraq, why should these military families vote for them?</P>
<P>If the only choices on offer are different versions of conservatism, it shouldn't be a surprise when voters choose the conservative Republican.</P>


