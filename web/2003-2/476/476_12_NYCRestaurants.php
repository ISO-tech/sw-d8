<P><font face="Times New Roman, Times, serif" size="4">Strike stops concessions</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Union win at restaurants in New York</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Tristin Adie</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 14, 2003 | Page 12 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">BUSBOYS, DISHWASHERS, waiters and bartenders at some of New York City's poshest restaurants united to beat back management attacks on their health care last week. Members of Hotel Employees and Restaurant Employees (HERE) Local 100 put up picket lines and a giant inflatable rat--familiar to many as a symbol of corporate greed--outside of the 21 Club and La Caravelle on November 4. </P>
<P>The 800 workers represented in Local 100's negotiations with 25 high-end restaurants voted overwhelmingly to authorize a strike October 25, a week before the expiration of their contract. They had spent more than a month demonstrating in front of their restaurants and leafleting patrons about the issues at stake.</P>
<P>Management for the restaurants demanded a number of concessions from the union. Citing a downturn in business after the September 11 attacks and rising health care costs, owners demanded that workers begin paying into their health care plans and agree to a 50 percent cut in vacation pay. </P>
<P>The bosses also threatened to reduce contributions to workers' pension funds. But workers made it clear that they couldn't afford these concessions. Management demands for health care contributions, for example, would have been $5 a week during the first year of the contract, and then $10 and $15 in the two years after that.</P>
<P>This would have hit busboys and dishwashers--who earn less than $400 a week--particularly hard. Pension contributions were also of great importance to older restaurant workers who make up the bulk of the workforce at a number of the restaurants. </P>
<P>According to members of the union's negotiating team, owners came into the talks crying poverty and claiming that they would be put out of business if they had to pay health care costs alone. Coming from restaurants that serve some of the highest-priced meals in the city, the claim was hard to believe. </P>
<P>The 21 Club, for example, is the seventh-highest-grossing restaurant in the country. But even according to their figures, the bosses' claims didn't add up. Members of Local 100's negotiating team looked over the numbers and found that they had been miscalculated--twice. </P>
<P>"One of the restaurant managers is a CPA [Certified Public Accountant] who sat with a calculator through the negotiations, and he never even caught the disparity," said Michael Slater, a shop steward at the 21 Club. "When we showed them their figures were wrong [the second time], we reached an agreement within five minutes." </P>
<P>But it was the level of preparation and organization of rank-and-file members that was decisive in forcing the owners' hand. Slater reported that not a single person crossed the picket lines at the 21 Club and La Caravelle. </P>
<P>The union made a priority of mobilizing its members in the weeks leading up to the strike. In addition to boisterous demonstrations and regular leafleting, stewards organized meetings at work sites to update members on the status of negotiations, and even held additional strike votes the night before setting up picket lines. As Slater put it, "Management was truly surprised by our solidarity."</P>
<P>Local 100 members are still involved in negotiations with a number of other restaurants, including the Oyster Bar, Caf&eacute; Des Artistes and Gallagher's Steak House, though they are reportedly close to a settlement. The willingness of workers at the 21 Club and La Caravelle to fight has made it much more likely that management at the remaining restaurants will settle, too. </P>
<P>Against the backdrop of workers' struggles for health care benefits in a number of cities, the restaurant workers' successful fight should be an inspiration to everyone. "I see our victory more broadly," Slater said. "It was connected with the 41 million uninsured people across America."</P>


