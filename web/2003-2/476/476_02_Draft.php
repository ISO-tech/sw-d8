<P><font face="Times New Roman, Times, serif" size="4">Pentagon floats a call for local draft board volunteers</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Return of the draft?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 14, 2003 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">IS THE Bush administration scheming to bring back the draft? That's the growing concern after the Defense Department recently posted a call on its Web site for volunteers to sit on local draft boards. According to the call, the Selective Service System "wants to hear from men and women in the community who might be willing to serve as members of a local draft board."</P>
<P>In the event of a national call-up, some 2,000 local draft and appeals boards would decide who goes into the military and who gets "deferred." Bringing back the draft--which was abolished in 1973 as the U.S. was beginning to admit defeat in the Vietnam War--would be a political disaster for Bush, particularly during an election year. </P>
<P>The fact that the Pentagon would even float the idea is a sign of the deep military mess that the Bush administration finds itself in. "When you crunch the numbers, you understand why you hear talk about a draft," Charles Pena, a senior analyst with the right-wing Cato Institute, told the <I>Toronto Star</I>. "You only have to look at troop levels to realize we don't have the numbers to do the job in Iraq properly."</P>
<P>The Bush administration originally promised that the bulk of U.S. troops would be home from Iraq by November, but in the face of an increasingly organized and effective resistance, many reservists have seen their tours extended, sometimes twice. Plus, countries like Turkey and Japan, which the Bush gang had counted on to send thousands of troops, are backing out.</P>
<P>No wonder a recent poll by the Army newspaper <I>Stars and Stripes</I> found that 50 percent of reservists surveyed said they wouldn't re-enlist. All of this means that the Bush administration may be facing a choice down the road between using the draft to find soldiers for its military--or see its occupation become even more stretched.</P>
<P>"They don't want us to have to do it," Dan Amon, a spokesperson for the Selective Service, admitted to <I>Salon</I> magazine. "But they want us to be ready to do it at the click of a finger." </P>
<P>During Vietnam, the draft system was notoriously racist and class biased. The wealthy and well-connected could buy their way out of service with deferments and service in the National Guard, while minorities and the poor ended up in the lowest--and deadliest--ranks of the armed forces. </P>
<P>From 1964 to 1973, 2.5 million people out of 27 million eligible draftees were sent to Vietnam. High school dropouts were three times more likely to be sent to the combat units that took the casualties. Blacks, who made up 12 percent of the total number of troops, often accounted for 25 percent or more of the combat units.</P>
<P>Today, though there may not be a formal draft, there's nothing fair about the composition of the military. That's because the "all-volunteer" military operates by what antiwar activists call a "poverty draft."</P>
<P>Minorities make up more than one-third of the military's 1.3 million service members. During the first Gulf War in 1991, more than 25 percent of U.S. soldiers were African American, about twice their proportion in the U.S. population as a whole.</P>
<P>The Pentagon spends more than $2 billion a year marketing the military to poor and working people as one of the few opportunities for a steady job. As the Central Committee for Conscientious Objectors states: "They take advantage of an economy that increasingly squeezes out those without a college degree, the gutting of college financial aid and the collapse of affordable housing."</P>
<P>The Bush administration may not try to bring the draft back before the election. But antiwar activists today should remember the movement against the Vietnam War, when the draft became a major target of demonstrations and resistance.</P>
<P>As Ned Lebow, a professor at Dartmouth College, told <I>Salon</I>: "If Congress and Bush re-institute the draft, it would be the '60s all over again. It's hard to imagine Congress passing such a bill, but then, look how many members of Congress just rolled over and played dead on the bill for $87 billion for Iraq and Afghanistan." </P>


