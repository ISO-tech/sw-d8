<P><font face="Times New Roman, Times, serif" size="5"><b>Antiwar protesters target Powell in NYC</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul Grohman</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 14, 2003 | Page 10 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--More than 250 people turned out at City College to protest a visit by Secretary of State Colin Powell. "My brother just came home and will likely be sent back in June," Elizabeth, a member of Military Families Speak Out, told protesters, "and this is all while Powell is being wined and dined at City College."</P>
<P>Some 20 groups endorsed the protest, including Parents for Peace, United for Peace and Justice-NYC, Vietnam Veterans Against the War, Campus Antiwar Network, the Student Liberation Action Movement, the ISO, ANSWER and the Harlem Tenants Council. Speakers included a Korean War veteran and member of Veterans for Peace who yelled to be heard above the noise of police helicopter overhead, "Colin Powell is the enemy of African people everywhere, whether here or in Africa."</P>
<P>One of the main messages of this protest was "Money for schools, not for war and occupation!" While the Bush administration pushes through congressional approval for $87 billion for the brutal occupation of Iraq, City University of New York schools face massive budget cuts.</P>
<P>With higher education budgets collapsing and student tuition soaring, and with the mounting loss of American and Iraqi life, this is a critical moment to demand a change in U.S. policy at home and abroad. We have to protest and Powell and the rest of Washington's warmarkers--wherever they appear.</P>
<I><P>Alan Wallis contributed to report.</P>
