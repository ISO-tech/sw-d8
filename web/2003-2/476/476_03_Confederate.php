<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Confederate flag furor exposes the Democrats' hypocrisy</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>No excuse for racism</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 14, 2003 | Page 3 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE CONTROVERSY over Howard Dean and the Confederate flag proves not only that Dean can't be trusted about issues of racial justice--but that his rivals for the Democratic Party presidential nomination can't either.</P>
<P>Dean is on the hot seat over his often-repeated comment that he wants to be "the candidate for guys with Confederate flags in their pickup trucks." Through most of last week, Dean refused to apologize, insisting that he didn't mean to endorse racism.</P>
<P>So what the hell <I>was </I>Dean saying? "My first impression was, gosh, does he not understand what that flag represents?" said Waring Howe Jr., a South Carolina lawyer and member of the Democratic National Committee. Of course he does. </P>
<P>Dean can't possibly be ignorant of what the Confederate flag stands for--slavery, the Ku Klux Klan, lynch mobs, fire hoses and police dogs unleashed on Black schoolchildren. Or that the right-wing effort today to keep the Confederate flag flying in Southern states serves as a stand-in for openly racist appeals that are no longer acceptable in mainstream politics.</P>
<P>How could an African American--or anyone else, for that matter--hear Dean's comments and not conclude that the frontrunner for the Democratic presidential nomination is, at best, indifferent to racism, and, at worst, willing to pander to the racists?</P>
<P>Still, many liberals leapt to Dean's defense, accepting his explanation that he wasn't supporting racism, but making a case that the Democrats had to win back working-class white voters in the South who have gone over to Republicans whose agenda is openly pro-corporate. But even if this excuse is genuine--and no one should forget that Dean considers himself a better "triangulator" than Bill Clinton, who was famous for adopting the Republicans' coded racist rhetoric about welfare and crime--the argument doesn't hold water.</P>
<P>First of all, the Confederate flag isn't a symbol of working-class status in the South. It's a symbol of racism--which is why Southern whites who reject racism <I>don't </I>put the Confederate flag on their bumpers.</P>
<P>Moreover, the idea of appealing to racist white workers simply on the basis of their common economic interests with Black workers ignores the importance of confronting racism, too. As long as racism goes unchallenged, white workers will be prey to right-wing politicians and union-busting employers who are willing to use scapegoating to advance their own interests.</P>
<P>This is why bigotry in any form can't be accepted, excused or overlooked, but must be taken on, however it appears--whether on a bumper sticker or coming from the mouth of a Washington politician. It is a sign of how far liberalism has retreated that Dean's statement could be defended by people who not so many years ago would never have tolerated any use--for whatever purpose--of such a notorious symbol.</P>
<P>But if Dean showed his true colors last week, so did the hypocrites who are his main rivals for the Democratic presidential nomination. These candidates grabbed headlines by criticizing Dean, but in most cases, you wouldn't have known that the disagreement was about racism.</P>
<P>The whole uproar was treated as if Dean had violated some rule of polite conversation--rather than exploited a highly charged symbol of bigotry. Rep. Dick Gephardt (D-Mo.) took the cake with his pathetic comment that he wanted to be the candidate "for the guys with American flags in their pickups." As if people who display the Confederate flag are wrong because they aren't <I>patriotic </I>enough.</P>
<P>But this shouldn't surprise anybody. Because they are, first and foremost, devoted to serving the same corporate interests as the Republicans, leading Democrats like Dick Gephardt don't actually care about organizing multiracial unity.</P>
<P>They will use the issue of civil rights and racism if they think it will help win votes--and nothing more. It's up to those who genuinely care about justice to make the case that Black and white workers can unite--but <I>only</I> on the basis of genuine equality and opposition to racial oppression in all its forms.</P>


