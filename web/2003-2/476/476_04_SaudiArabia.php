<P><font face="Times New Roman, Times, serif" size="4">An unprecedented protest for reform in Riyadh</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Cracks in Saudi society</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 14, 2003 | Page 4 </font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</I>, <BR>
</B>Saudi Arabia has long been known for its iron-fisted dictatorship--no elections, no free speech, no rights for women and immigrants. And through it all, the U.S. has been a steady ally.</P>
<P>But on October 14, something unprecedented happened. Over 150 people protested for reform in the center of the capital, Riyadh, blocking roads and snarling traffic. It was not a huge protest, but what made it so important was the fact that Saudi Arabia has never seen open protest against the government in its 70-year history.</P>
<P>After the protests, Crown Prince Abdullah vowed that "this won't happen again." But Saudi Arabia has recently come under tremendous strain, both inside and out. Spontaneous actions of support of Palestinians, as well as terrorist attacks in the capital in July, shattered the country's veneer of stability.</P>
<P>In the last 20 years, the population of Saudi Arabia has skyrocketed. Now, a country once able to use its huge oil revenues to provide decent living standards for the majority of its workers is rapidly becoming polarized along class lines. One significant result of this shift is the hesitancy of the government to support U.S. military operations in the region, for fear of popular opposition. </P>
<P>Saudi Arabia's workforce has gone from being small and pampered to having a mighty and angry presence that, if organized, could threaten the monarchy's survival. The last thing that the U.S. would want to see is the downfall of a ruling clique that has kept the world's largest proven oil reserves safe for Uncle Sam. </P>
<P>This demonstration, though very small, has opened a critical crack in an angry Saudi society that sees the government preaching anti-Western, anti-Israel rhetoric while collaborating with U.S. imperialism. Some kind of more radical change may not be far behind--and this will create further problems for Washington's plans to dominate the Middle East.</P>
<B><P>Mitch Lewis, </B>Boston</P>

