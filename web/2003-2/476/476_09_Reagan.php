<P><font face="Times New Roman, Times, serif" size="4">Right wing in a frenzy over Reagan movie</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>That ain't the half of it</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Phil Gasper</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 14, 2003 | Page 9 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">IN A stunning act of cowardice last week, CBS canceled its much-publicized "docudrama" <I>The Reagans, </I>about former president Ronald Reagan, caving in to a campaign by the Republican National Committee, right-wing radio hosts, Fox News and conservative Internet sites.</P>
<P>The movie will now be shown to a much smaller audience on the Showtime cable network next spring. Conservatives attacked the film for portraying Reagan as homophobic, and his wife Nancy as a domineering wife and mother who pulled the strings behind the scenes and abused her children. They were incensed that James Brolin, husband of liberal icon Barbra Streisand, plays the part of Reagan.</P>
<P>While by all reports <I>The Reagans </I>is a monumental example of third-rate TV schlock, examples cited by conservatives of substantial inaccuracies don't hold up. One complaint was that the movie shows Reagan ignoring the AIDS crisis because of its association with gay sex, and telling his wife, "They that live in sin shall die in sin."</P>
<P>But in real life, Reagan refused to mention AIDS publicly for six years, under-funded federal programs dealing with the disease and, according to his authorized biography, said, "Maybe the Lord brought down this plague," because "illicit sex is against the Ten Commandments."</P>
<P>C. Everett Koop, Reagan's surgeon general, later revealed, "because transmission of AIDS was understood primarily in the homosexual population and in those who abused intravenous drugs, the advisors to the president took the stand, they are only getting what they justly deserve."</P>
<P>In the movie, Nancy slaps her 5-year-old daughter, Patti. In real life, Patti wrote, "I first remember my mother hitting me when I was eight. It escalated as I got older and became a weekly, sometimes daily, event."</P>
<P>In the movie, Nancy insists, "Ketchup is a vegetable! It is not a meat, right? So <I>it is </I>a vegetable." In real life, Reagan directed the Department of Agriculture to classify ketchup as a vegetable in September 1981 in an attempt to slash $1.5 billion from the federal school lunch program.</P>
<P>Conservatives also criticized the movie for what it does not include. "Does it show he had the longest and strongest recovery in postwar history?" asked Reagan's White House press secretary Marlin Fitzwater.</P>
<P>But Reagan's economic policies were a disaster for working-class Americans. Reagan presided over the worst recession since the 1930s, and economic growth in the 1980s was lower than in the 1970s. By the end of the decade, the poverty rate had increased by 20 percent.</P>
<P>The real problem with <I>The Reagans </I>is not that it is too critical of the Reagan presidency, but that it is largely uncritical. According to <I>The New York Times, </I>the movie "paints [Reagan] as an exceptionally gifted politician and a moral man who stuck to his beliefs, often against his advisers' urgings."</P>
<P>Reagan was many things, but "gifted" was not one of them. "Poor dear," remarked British Prime Minister Margaret Thatcher, his closest international ally, "there's nothing between his ears."</P>
<P>As for a "moral man," Reagan's morality included union busting--beginning with his dismissal of striking air traffic controllers in 1981--an unprecedented war on the poor, opposition to civil rights and support for apartheid South Africa. The "moral" Reagan trained and supported terrorists, including the Nicaraguan contras ("the moral equal of our Founding Fathers") who killed over 30,000 people, and Islamic radicals in Afghanistan who later formed the al-Qaeda network.</P>
<P>Reagan was also a liar. In November 1986, he publicly denied that his administration had been illegally selling arms to Iran and using the proceeds to fund the contras. One week later he was forced to retract this statement, but denied that the sale was part of a deal to free U.S. hostages. The following year, Reagan admitted that there had been an arms-for-hostages deal, but denied he knew anything about it.</P>
<P>In 1992, that too proved to be a lie when former Defense Secretary Caspar Weinberger released notes from a January 1986 meeting revealing, "President decided to go with Israeli-Iranian offer to release our 5 hostages in return for sale of 4,000 TOWs [U.S. missiles] to Iran by Israel."</P>
<P>An honest account of Reagan's life would cover these things. But if our craven mass media isn't even prepared to show <I>The Reagans </I>in prime time, don't expect to see a movie that really tells the truth about Ronald Reagan anytime soon.</P>


