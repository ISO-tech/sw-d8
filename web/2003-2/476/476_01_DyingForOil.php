<P><font face="Times New Roman, Times, serif" size="4">Bush's occupation of Iraq</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Dying for oil and empire</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 14, 2003 | Page 1 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE RETALIATION was swift--and brutal. "Houses shook, walls cracked, chandeliers swayed, and children woke up screaming for their parents as U.S. planes dropped 500-pound bombs on the outskirts of Saddam Hussein's hometown overnight," reported the Associated Press.</P>
<P>The show of force was a warning to the 120,000 Iraqis living in the city of Tikrit not to support the guerrilla resistance to the U.S. occupation--which just hours earlier had shot down a Black Hawk helicopter, killing six U.S. soldiers. A few days earlier, an Army CH-47 Chinook helicopter was downed outside of Baghdad, killing 16 soldiers and wounding 20.</P>
<P>In all, 34 U.S. troops died in the first 10 days of November--the bloodiest period for Washington since George W. Bush declared victory in Iraq half a year ago. And the U.S. response?  More raids. More bloodshed. More humiliation for ordinary Iraqis. As Lt. Col. Steven Russell said of the U.S. attacks in Tikrit, "This is to remind the town that we have teeth and claws, and we will use them." </P>
<P>Horrors like this seem to follow every attempt by the Bush administration to put a new spin on its colonial occupation of Iraq. The day before the Black Hawk was shot down, Bush unveiled the latest justification for invading Iraq.</P>
<P>Forget weapons of mass destruction or the "liberation" of a grateful people. The real purpose, Bush now insisted, was to promote democracy by making Iraq a model for the region to follow--"a forward strategy of freedom in the Middle East," he declared.</P>
<P>Successful democratic societies, Bush lectured, "prohibit and punish official corruption and invest in the health and education of their people." Of course, the Iraqi Governing Council, handpicked by Washington, is so decrepit and corrupt that U.S. overseers are considering whether they should scrap it. As for "health and education," schools in Baghdad are having a hard time keeping their doors open--because parents are too scared to send their children for fear of the daily bombings.</P>
<P>Meanwhile, as <I>New York Times</I> reporter Dexter Filkins described it, "a typical hospital in Iraq is a nightmarish place where they don't have electricity yet. Where there's people sleeping on the floors; where the emergency rooms at night are flooded with people who have been shot and maimed in the chaos that breaks out after curfew."</P>
<P>Democratic countries "privatize their economies and secure the rights of property," Bush blustered. But the only people who seem to have the "rights of property" in Iraq these days are the U.S. corporate vultures at Bechtel and Halliburton.</P>
<P>No excuse can cover up the truth--that the resistance to the U.S. occupation of Iraq is growing larger and more organized. And while the Pentagon brass and Bush administration hawks give the orders, increasing numbers of U.S. soldiers are asking why they are the ones whose lives are at risk.</P>
<P>"I have no idea why we're here," Army Specialist Jeremy Polston, a self-described Bush Republican stationed in the city of Ar Ramadi, told <I>Newsweek </I>magazine. Polston now has a fragment of shrapnel lodged in his leg from a bomb blast--and a sign hanging above his bunk that reads "One weekend a month my ass!"</P>
<P>No more lives should be sacrificed for oil and empire. It's time to stand up and say enough is enough. </P>



