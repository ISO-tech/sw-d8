<P><font face="Times New Roman, Times, serif" size="4">Mechanics defy Los Angeles transit authority</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"We're sticking together"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Bruce Cooley</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 14, 2003 | Page 11 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">LOS ANGELES--In a show of defiance, striking transit mechanics and maintenance workers overwhelmingly rejected the Los Angeles Metropolitan Transit Authority's "last, best and final" offer last week. By a vote of 1,267 to 87, Amalgamated Transit Union (ATU) Local 1277 members rejected the offer and vowed to continue their nearly four-week-long strike that has brought to a halt LA's trains and buses.</P>
<P>"We're staying together for health care and a living wage," Dave, a mechanic and 23-year member of ATU, told <I>Socialist Worker</I>. "We've been going back and forth with [the MTA] for 16 months, and they had nothing new" in the latest offer, he said.</P>
<P>The MTA's offer included no raises next year, with 1.5 percent raises in the second and third years of the five-year contract. While the MTA says they would increase contributions to members' health fund and monthly premiums, the increases wouldn't come close to making up for years of rising costs unmatched by MTA's contributions.</P>
<P>Future rises in health care costs would hit members hard. "I'll be paying a couple hundred dollars a month for health care from the beginning, and more down the line" Dave explained. "To accept what they give us, we would become the working poor."</P>
<P>LA's transit bosses are out for union blood. The MTA walked out of negotiations last week, declaring an impasse and calling for a vote on their offer.</P>
<P>By declaring an impasse, the MTA has prepared the ground to impose the terms of their offer and raised the threat of hiring replacement workers. The MTA says it isn't planning to hire replacement workers yet, but says it has nothing more to offer.</P>
<P>The MTA's claims of poverty ring hollow to workers. "Politicians are getting their raises," said Dave. And MTA Chief Executive Roger Snoble, who makes more than $280,000 a year, is one of the highest paid transit CEOs in the U.S.</P>
<P>ATU Local 1277 President Neil Silver, who had initially refused the MTA's demand to call a vote, changed course last week and called for members to vote "no." Silver is pressing his case for arbitration, in which mechanics would return to work while a panel resolves the dispute.</P>
<P>The MTA board has rejected calls for arbitration, but this may change following the "no" vote. But arbitration in no way promises to defend the livelihoods of the ATU's 2,800 members. "They have the judicial system and stacks of millions," to use to defeat the strike, Dave told <I>Socialist Worker</I>.</P>
<P>Building solidarity with other unions and tapping into the broad public support for the demands of the workers is the way to win this strike. </P>



