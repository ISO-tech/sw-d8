<P><p><font face="Times New Roman, Times, serif" size="4"><b>Chicago Transit Authority</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Lauren Fleer</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--For four years now, 6,600 Chicago transit employees have been working without a contract. Union officials have been negotiating intermittently with the Chicago Transit Authority (CTA) in the years since the membership voted down CTA's last offer.</P>
<P>The International leadership of the Amalgamated Transit Union assumed control of Local 241 this February, after former President Lee Robinson signed a contract settlement without approval from the union's executive board. Arbitration between CTA officials and union representatives began this August, but no progress has been reported. </P>
<P>"We've waited four years, working every day, and we want our contract," driver Michael Taylor told <I>Socialist Worker.</I> "We want a fair wage." Taylor stressed that the $20.01 per hour that bus drivers earn is well deserved. "We put ourselves in harm's way every day to do our jobs," said Taylor. "We shouldn't have to go seven years without a raise."</P>
<P>A new round of bargaining between bus drivers and CTA management is scheduled to start in December--at a time when the CTA is forecasting a $50- to $80-million budget deficit for next year. </P>
<P>CTA President Frank Kruesi has proposed a citywide fare increase to make ends meet--even as he slipped through a bundle of pension enhancements for himself and top CTA managers without public hearings or discussion. Drivers have since become concerned about the solvency of their pension fund, which is managed by a joint union-management pension board, and are currently trying to get rank-and-file members seats on the board.</P>
<P>Drivers and riders alike are also incensed about the potential fare hike. "How can they be hiking fares when they haven't even taken care of their workers?" remarked Denise Dixon, field organizer for Jesse Jackson's Operation PUSH Coalition, which supports the workers. "It's just disrespectful--they can take care of their own bottom line, but the workers get nothing."</P>
<P>Management, armed with a no-strike clause, seems to have had the upper hand for several years. Now's the time to step up the pressure and make common cause with riders angry over fare hikes in order to push CTA to deliver a new contract and a raise.</P>

