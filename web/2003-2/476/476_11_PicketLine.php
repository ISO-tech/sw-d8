<p><a name="Tyson"></a><font face="Times New Roman, Times, serif" size="4"><b>Tyson Foods</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Nathan Fuller, Bill Linville and Ryan Spangler</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">JEFFERSON, Wis.--Nearly 400 strikers and supporters turned out for a November 7 rally in support of United Food and Commercial Workers (UFCW) Local 538's eight-month-long strike against Tyson Foods. Featured speakers included AFL-CIO President John Sweeney and prominent state politicians.  </P>
<P>"This is not a fight against one greedy plant in Jefferson, this is a fight against corporate greed in America," said Sweeney. But despite Sweeney's fighting rhetoric, UFCW and AFL-CIO leaders still haven't called a nationwide boycott of Tyson products for fear of a lawsuit.   </P>
<P>The only strategy that union leaders seem to have can be summed up by their main slogan--"one day longer, one day stronger." But this gives the advantage to Tyson, a multibillion-dollar corporation with pockets deep enough to outlast workers who must pay rent, bills, mortgages and health care costs.</P>
<P>"How many rallies are they going to have?" asked striker Chuck Moehling. "In the next two to three months, something has to happen." At this critical juncture, Tyson workers will have to look to labor's fighting traditions for a strategy that can win.</P>
<P>In 1989, for example, strikers at Pittston Coal ignored more than $13.5 million in fines and occupied the plant for 77 hours after being on strike for a full year. This action forced Pittston to come back to the table, saving their union <I>and</I> forcing all previously levied fines to be dropped.</P>
<P>Rank-and-file workers have to begin organizing meetings to hammer out a strategy to force Tyson to the table.</P>

