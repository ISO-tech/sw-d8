<p><a name="Rochester"></a><font face="Times New Roman, Times, serif" size="4"><b>Rochester, N.Y., transit </b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Brian Lenzo</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">ROCHESTER, N.Y.--About 120 transit workers from Amalgamated Transit Union (ATU) Local 282 and supporters picketed outside the city bus depot to demand a contract from management. The crowd was a cross section of bus drivers, mechanics, janitors, technicians, representatives from local unions and supporters picketing in solidarity. </P>
<P>Workers have been without a contract since their last one expired April 1, 2002. After 22 months and 26 arbitrations, management's best offer contains no wage increase in the first year of the contract and a measly 3 percent increase in subsequent years.  </P>
<P>Management also wants to create a two-tier health benefit system, putting new hires on a less expensive plan. "It's one thing after another," said one picketer. "They say they don't have money, but their books show that they're taking in millions."</P>
<P>Because of New York's anti-worker Taylor Law, public employees are prohibited from striking, and penalties for violations are levied against the union and employees involved. Like many workers around the country, transit workers face a struggle where all the rules rigged to work against them. </P>
<P>However, spirits remain high. "They're saying that we're unskilled and unprofessional," explained another picketer. "We're getting ripped off--that's why we're out here."</P>

