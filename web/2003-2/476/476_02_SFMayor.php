<P><font face="Times New Roman, Times, serif" size="4">Left-winger makes runoff election for mayor</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Green candidate stuns S.F. Democrats</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Todd Chretien</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 14, 2003 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO voters shocked the Democratic Party establishment in the November 4 mayoral election by giving Green Party candidate Matt Gonzalez 20 percent of the vote, putting him in a December 9 runoff. Gonzalez, who was elected to the city's Board of Supervisors (or city council) as a Green in 2000, came in second to moderate Democrat Gavin Newsom, a rich businessman who plowed millions into a string of exclusive restaurants and buying himself a political career.</P>
<P>Newsom has rallied San Francisco's business elite and leisure yuppie class behind him with a series of campaigns to sweep the homeless off the streets and into prison. Gonzalez beat out two longtime San Francisco liberal Democrats in the first round by mobilizing a new generation of voters and sponsoring a successful referendum to raise the city's minimum wage from $6.75 an hour to $8.50.  </P>
<P>Gonzalez is an outspoken opponent of the occupation of Iraq and addressed the 15,000-strong antiwar protest in San Francisco October 25, saying "the only way to end the occupation and fight for justice is to build a strong social movement."  He supports giving undocumented immigrant workers the right to vote in school board elections, a radical reform that would give the immigrants rights movement a major victory.</P>
<P>As an elected official, Gonzalez has at times contradicted his own principles. For instance, during the mass direct action protests last spring after Bush invaded Iraq, Gonzalez chided demonstrators to "go protest in a city that hasn't already passed an antiwar resolution"--and supported the police arresting thousands of demonstrators blocking city streets and corporate headquarters.</P>
<P>And Gonzalez defended a raise in the Board of Supervisors' salaries from $37,000 a year to $117,000--at a time when unionized city workers suffered a 7 percent pay cut because of the budget crisis. Despite his mistakes, though, this election is shaping up into a battle, as Gonzalez puts it, over "two opposing philosophical views."  </P>
<P>Ultimately, the runoff will pit a well-heeled Democrat who wants to climb to power in the richest city in the country on the backs of its homeless against a progressive Green who stands for defending working-class tenants against evictions and making large corporations pay a heavier share of taxes.</P>
<P>The election could have an influence on the 2004 presidential race, as 2000 Green Party presidential candidate Ralph Nader is said to be watching the San Francisco results closely. A strong showing by Gonzalez could give more confidence to Green Party and independent activists who believe that if it was right to challenge the two-party system in 2000--and it remains right in 2004.  </P>


