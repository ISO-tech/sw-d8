<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Washington's Medicare swindle</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 14, 2003 | Page 3 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">LEAVE IT to Congress to come up with a Medicare cure that's worse than the sickness. Sometime this week, the House and Senate are supposed to reconcile their different versions of Medicare "reform" legislation. Politicians of both parties are hyping the deal as a way to deliver a prescription drug benefit for seniors and disabled people who currently have to pay out-of-pocket or rely on costly private insurance coverage for their prescriptions.</P>
<P>But neither Democrats nor Republicans are insisting on funding the program adequately. George W. Bush and his allies among the Republican leadership in the House want to limit spending on a prescription drug benefit to $400 billion over 10 years. But this is only a fraction of the $1.8 trillion that seniors and the disabled will pay for prescription drugs for the next decade, according to the Congressional Budget Office.</P>
<P>The proposal does make some people happy--in particular, the CEOs of big pharmaceutical companies. That's because 61 percent of the new Medicare dollars to be spent on prescriptions would end up as profits, free and clear, for the drug makers, according to Alan Sager and Deborah Socolar, directors of the Health Reform Program at the Boston University School of Public Health.</P>
<P>"This windfall means an estimated $139 billion in increased profits over eight years for the world's most profitable industry," write Sager and Socolar. "At $17 billion annually, this means about a 38 percent rise in drug maker profits." </P>
<P>The drug coverage sellout is only one part of the politicians' Medicare swindle. In negotiations over the final form of the legislation, House Republicans are refusing to budge on their fanatical devotion to the ideology of the "free market." They'd like to privatize the government-run Medicare system outright to help out their pals in Corporate America.</P>
<P>But since that's not politically feasible, they're trying for their next best option--incorporate "market mechanisms" into the existing Medicare program. The Republican delusion is that making the Medicare program compete with private insurance companies would increase efficiency and "bring down costs."</P>
<P>This is a fraud. Based on the track record of Medicare HMOs that stretches back 20 years now, privatization schemes have been a catastrophic failure. Medicare HMOs are less efficient, not more, spending an average of 15 percent of revenues on administrative costs, compared with 2 percent in the government-run system. And they cost more. In 2001, someone in good health enrolled in a Medicare HMO spent a hefty $1,786 in out-of-pocket expenses each year. For those in poor health, the out-of-pocket cost was $4,783.</P>
<P>With broad public support for prescription drug coverage and opposition to further moves toward privatization, you'd think the Democrats would take the offensive to challenge the Republicans' assault on Medicare. But while liberal Democrats like Sen. Edward Kennedy (D-Mass.) oppose the details of the Republican plan, they conceded all the basic principles months ago when they voted for Medicare "reforms"--claiming that they could fight later for improvements, as long as the prescription drug benefit went through.</P>
<P>This Medicare "reform" will take a bite out of seniors and disabled people--some of the most vulnerable people in society. The only solution to the health care crisis in the U.S. is a government-run single-payer health care system that puts people before health care industry profits.</P>



