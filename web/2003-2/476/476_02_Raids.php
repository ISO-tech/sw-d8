<P><font face="Times New Roman, Times, serif" size="5"><b>Armed cops raid S.C. school</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 14, 2003 | Page 2 </font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE COPS charged in, guns drawn. The "perps" were brought under control within 30 seconds and forced to lie face down. About a dozen were handcuffed for not complying quickly enough.</P>
<P>Who were these hardened criminals taken down by 15 police officers in Goose Creek, S.C.? The students of Stratford High School.</P>
<P>"They would go put a gun up to them, push them against the wall, take their book bags and search them," student Aaron Sims told reporters. "I was scared." More than 100 students were caught in a main hallway that the school's principal invited police to "sweep."</P>
<P>"I'm absolutely outraged," said Danny Partin, a parent of one of the students. "This is supposed to be a free country, not a police state." After parents' complaints and media attention, South Carolina state police launched an investigation into the heavy-handed tactics of the Goose Creek Police Department.</P>
<P>"It sounds like the police and the principal forgot what country they're living in," said Ethan Nadelmann, executive director of the Drug Policy Alliance. "Sending armed troops into schools creates far more danger than any potential threat of drug use...</P>
<P>"The local police and this principal...should resign immediately and be diverted into a refresher course on the Bill of Rights. If ever we needed evidence that the war on drugs has gone too far, this is it."</P>


