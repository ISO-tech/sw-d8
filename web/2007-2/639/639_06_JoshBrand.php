<font face="Times New Roman, Times, serif" size="5"><b>Sympathy for a sad loss</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 3, 2007 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear friends and family of Joshua Brand</b>,<br>
I never knew Joshua, but I've been reading <i>Socialist Worker</i> for nearly three years. I felt moved by your loss and knew that I had to write in ("In memory of Joshua Brand," July 6).<p>

<table width="270" align="right"><tr><td>
<table width="258" cellpadding="12" cellspacing="0" align="right" border="2" hspace="8" bordercolor="006699" bgcolor="#FFCC66"><tr><td>
<font face="Arial, Helvetica, sans-serif" size="4" color="#990000"><b>In memory of Josh</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">THE LOSS of Joshua Brand, a dedicated fighter in the struggle for a better world, was a blow to our movement and to those who knew and loved him.<p>
The Center for Economic Research and Social Change, publisher of Haymarket Books, is accepting donations in memory of Josh to support Haymarket, a project that Josh supported and saw as important to our struggle.<p>
If you would like to contribute, please send checks payable to CERSC (with "In memory of Josh Brand" written in the memo line) to: CERSC, P.O. Box 258082, Chicago, IL 60625. For more information, call 773-583-7884.
</td></tr></table>
&nbsp;<p>
</td></tr></table>

<font face="Times New Roman, Times, serif" size="3">I'm so very sorry to hear about his passing and I wanted to express my deepest sympathy. He sounded like a beautiful soul. I especially wanted to express my sympathy to Jenni Rowe. Her letter broke my heart. I'm so very, very sorry for your loss.<p>
Personally, I think a great way to honor his memory would be to write that article for the <i>International Socialist Review</i> in his memory. Do what your heart tells you, but I think it's a good idea.<p>
I'm finally going home soon, and once home, I'll be joining the International Socialist Organization. I'll fight for a better world in which people will be treated better and taken care of.<p>
With deepest empathy,<br>
<b>Christopher Henderson</b>, Low Security Correctional Institution, Butner, N.C.<p>

