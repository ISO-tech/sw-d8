<font face="Times New Roman, Times, serif" size="5"><b>The anger and alienation that feed racism</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Jason Farbman</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 3, 2007 | Page 11</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i><a href="http://www.thisisenglandmovie.co.uk/" target="_blank">This Is England</a>, </b></i>written and directed by Shane Meadows, starring Thomas Turgoose and Stephen Graham.</font><p>

<font face="Times New Roman, Times, serif" size="3">PART <i>ABOUT a Boy</i> and part <i>Romper Stomper,</i> writer and director Shane Meadows has created a masterful study of the British working class in <i>This Is England.</i> The film follows Shaun, a lonely 12-year-old who has just lost his father to the Falklands War, as he befriends a group of skinheads in a bleak, working-class rural England of 1983.<p>
Friendless and picked on, Shaun runs into a few older kids one day on his way home from school. These new friends--one of whom is Jamaican--dress as skinheads, but are not racist. Their leader, Woody, is kind and generous and brings Shaun into the group. Soon the boy is transformed into a pint-sized skinhead and surrounded by friends. As he is dropped off at home one evening, he declares to his new friends that it's been the best day of his life.<p>
 One night Woody's friend Combo is released from jail, and everything changes. He is much older--in his early thirties--and is seething with anger. Despite claims that he isn't racist, the group is clearly made uncomfortable by his descriptions of Jamaicans and others in jail. Once his racism becomes undeniable, Woody and many of the others break ties at once. They leave without Shaun, however, whose emotions are manipulated into making his father's death in the Falklands "mean something."<p>
The film is book-ended with footage from that war. This is not just to set the period, as Meadows has commented: "when you look at the footage, and see the [media] campaign as the unemployment figures hit 3 million, it does make you incredibly suspicious as to what paratroopers were doing fighting 16-year-old kids from Argentina.<p>
"It was an incredibly suspicious war, in the same way America and the UK got involved in Iraq. People can see that now. Obviously there were more people against going into Iraq than there were going into the Falklands...but the shame I carry as a British resident, was that it was a war handled in the media as if it were a World Cup summer."<p>
Not only does the footage make a connection between imperial wars abroad and the poor living conditions for the majority at home, it draws a straight line between what we are watching and what we are living through today. At the heart of <i>This Is England</i> is a working class left to rot; despite all proclamations to the contrary by politicians or mainstream media, people are struggling and are angry.<p>
In the movie, as is the danger now, there are those who present conclusions that only serve to divide the working class against itself. Nowhere is this better represented than in a pivotal scene where Combo lashes out against unemployment in England, the lies of Margaret Thatcher, and a war "about nothing," where "good people, real people" are being sent to die in battles "with a bunch of shepherds."<p>
He acknowledges that the competition between immigrants and native-born workers drives down wages, but instead of concluding that Thatcher and those in power are the beneficiaries, he falls into the trap of blaming immigrants for the country's problems. And while many reject his conclusions, there are those who follow him.<p>
<i>This Is England</i> takes a hard look at life in the working class. Far from condemning people for their own terrible circumstances, it attempts to explain how there are any number of conclusions one can be drawn into that cannot lead to a better life for anyone.<p>
At countless points in history racism and xenophobia have been used to divide an angry, bitter working class. These tactics are being employed today to keep a rapidly leftward-shifting populace at bay, and the fight against racism and xenophobia will again be one of the most significant struggles in which an organized left will need to engage.<p>

