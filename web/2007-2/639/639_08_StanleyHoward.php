<font face="Times New Roman, Times, serif" size="5"><b>A challenge to Rick Perry</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 3, 2007 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">STANLEY HOWARD was tortured by Chicago police, wrongfully convicted and sentenced to death, but in January 2003, he was pardoned by former Gov. George Ryan. Stanley is a member of the Campaign to End the Death Penalty and writes regularly for its newsletter, the <i>New Abolitionist</i>. He wrote this statement as part of the campaign for Kenneth Foster.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">THERE HAS always been a time in human history when someone stepped forward courageously to challenge the powers that be or the status quo on behalf of the oppressed and voiceless.<p>
Though it's been a long and hard battle, time and time again, overwhelming evidence has proved that the death penalty is barbaric and has no place in a civilized society. There is no escaping the reality that the criminal justice system and its machinery of death are plagued with errors, racism and injustices. Moreover, the odds of executing the innocent are just too great to maintain.<p>
In the face of all the facts and evidence, the world is condemning the death penalty, like it did South Africa's apartheid, but the death penalty remains in the United States, reserved for people of color and the poor.<p>
The tide has turned on the death penalty's popularity, and Texas is standing alone as the world's ground zero in this bloody and primitive practice.<p>
The time is now and very ripe for Gov. Rick Perry to step on the world's stage and do what he and the world knows to be right. It's time for him to hear the cries of the oppressed and voiceless (and those in need of mercy and justice) by no longer supporting this inhumane madness.<p>
With one stroke of a pen, he can shut down the world's most active human slaughterhouse. With one stroke of a pen, he can march the world closer toward the inevitable--total abolition of the death penalty.<p>
I make this plea and challenge to Gov. Perry on behalf of Kenneth Foster, Rodney Reed, members of DRIVE, and all the human beings who are suffering mentally, emotionally and physically on Texas' death row and death rows around the world. Take this courageous step and commute all of Texas' death sentences, and etch your name in human history by standing on the side of righteousness.<p>

