<font face="Times New Roman, Times, serif" size="5"><b>Hazelton's anti-immigrant law overturned</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 3, 2007 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">ONE YEAR ago, Hazelton, Pa., was ground zero for the attack on undocumented workers.<p>
Today, it's the site of a defeat for anti-immigrant forces after a judge ruled the town's draconian "Illegal Immigration Relief Act" was unconstitutional.<p>
The Hazelton ordinance made it illegal to rent to or hire undocumented workers. City officials were empowered to suspend the licenses of businesses who hired the undocumented and fine people who housed these workers or aided them in any way. The ordinance also made English the city's official language.<p>
The Hazelton ordinance set the tone for politicians across the country who wanted to capitalize on whipped-up hysteria about illegal immigration. Attempts to pass similar legislation were made in an estimated 80 different municipalities.<p>
"I believe Hazelton, Pa., is the model for governments across this nation," F. Steve Jenkins, a town council member in Culpeper, Va., told the <i>Washington Post </i>last September, as that town passed its own anti-immigrant measure.<p>
But in his decision last week, U.S. District Judge James Munley concluded, "Even if federal law did not conflict with Hazleton's measures, the city could not enact an ordinance that violates rights the Constitution guarantees to every person in the United States, whether legal resident or not.<p>
"The genius of our Constitution is that it provides rights even to those who evoke the least sympathy from the general public. In that way, all in this nation can be confident of equal justice under its laws...<p>
"The United States Supreme Court has consistently interpreted [the 14th Amendment] to apply to all people present in the United States, whether they were born here, immigrated here through legal means or violated federal law to enter the country."<p>
Witold Walczak, legal director of the ACLU of Pennsylvania and a lead attorney in the case, hailed the decision and underscored the real intention of the ordinance. "Hazleton-type laws are designed to make life miserable for millions of immigrants," he said. "They promote distrust of all foreigners, including those here legally, and fuel xenophobia and discrimination, especially against Latinos."<p>
Walczak's point was made obvious during the two-week trial, when city officials tried to claim that undocumented immigrants were to blame for an increase in Hazelton's crime rates and alleged gang activity--despite statistics to the contrary.<p>
Mayor Lou Barletta wrote up the legislation last year in an attempt to focus anger over a shooting in a city playground. "Illegal aliens in our city create an economic burden that threatens our quality of life," he wrote on his Web site. "With a growing problem and a limited budget, I could not sit back any longer and allow this to happen. I needed to act!"<p>
The Hazelton ruling sets a precedent for similar laws in other municipalities, several of which have been put on hold awaiting the Pennsylvania result. There is also the possibility that Munley's decision will be appealed--Barletta had promised as much--and taken to the Supreme Court.<p>
Immigrant rights activists have organized to confront Hazelton-type ordinances. For instance, in Carpentersville, Ill., activists and community members held protests that helped hold back anti-immigrant laws.<p>
The recent ruling in Pennsylvania should give activists added confidence to turn the tables on anti-immigrant attacks wherever they happen.<p>

