<p><a name="Orleans"></a><font face="Times New Roman, Times, serif" size="4"><b>The real failure in New Orleans</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I WOULD just like to put a dent in the Hurricane Katrina myth: It was Lake Pontchartrain that drowned New Orleans, because of levees that the Army Corps of Engineers were responsible for, and the city didn't even sound the claxon when they broke.<p>
It wasn't the hurricane. Giving nature all the credit is just wrong. The drowning of New Orleans was a failure of infrastructure--of people.<br>
<b>Tina</b>, from the Internet<p>

