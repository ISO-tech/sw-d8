<a name="Jena"></a><font face="Times New Roman, Times, serif" size="4"><b>Justice for the Jena Six</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I RECENTLY heard an interview with Marcus Jones, the father of Mychal Bell--one of the "Jena Six," a group of young Black men being railroaded on assault charges after they were the victims of a series of racist incidents in Jena, La.--on Madison's "Radio of the Free," and saw him interviewed on <i>Democracy Now!</i><p>
I am severely outraged by the case of the Jena Six. It's like the civil rights movement just completely passed over this small town in Louisiana. The "n word" still seems to be nonchalantly used by whites. Nooses were hung in a tree at the local high school, and there were few repercussions--in fact, Black students that complained were told basically to shut up about it.<p>
From the initial charges of attempted murder--for using a shoe during a fight!--to the charging of Black students with $500 fines for "theft" because they took a shotgun from a white man for fear of being shot by him, every turn that the story around this case takes is more disturbing and ludicrous than the next.<p>
We need to fight this injustice. This is only one particularly horrid case of racist injustice today, but we need to seriously do something to save the Jena Six. We can't keep letting these cases blow over after a short period of outrage.<p>
We need a new civil rights movement that connects Hurricane Katrina, the case of Gary Tyler and every case of this nature. May the Jena Six case be the lighting rod for a new generation that refuses to sit down and shut their mouths.<p>
In the 1930s, the American Communist Party fought to defend the Scottsboro Boys in Alabama through a grassroots campaign (and they won!). We should do the same today for the Jena Six in Louisiana.<br>
<b>Julie Southerland</b>, Greensboro, N.C.<p>
