<p><a name="Sexism"></a><font face="Times New Roman, Times, serif" size="4"><b>Who benefits from sexism?</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THANKS TO John D. for raising a very important question about women's oppression ("Are men to blame for sexism?" June 8).<p>
Sexist behavior by working-class men should never be excused by socialists just because, as John points out, socialists understand the root of women's oppression to be in the rise of class society. Surely no socialist would argue that a man who rapes a woman or a husband who assaults "his" wife isn't oppressing women unless he's a member of the bourgeoisie!<p>
Moreover, such acts of abuse should be strongly condemned by socialists--as <i>Socialist Worker</i> has done with the botched Duke lacrosse rape case (while the mainstream media has portrayed the frat boys as the "victims"). Socialists also stood alongside the Rutgers women's basketball team as they toppled Don Imus for his sexism and racism.<p>
There is a link between the racist, sexist assault by the Duke lacrosse players and the racist, sexist climate reinforced by the likes of Imus. It doesn't take a socialist perspective for anyone to realize that demeaning women in the media contributes to violence against women.<p>
What is unique about the socialist critique of the origins of sexism is socialists believe that most men--namely, working-class men--do not benefit in the oppression of women, even while some participate in that oppression.<p>
In Elizabeth Schulte's article last year, "How will women be liberated?" (March 24, 2006), she reports, "According to the Institute for Women's Policy Research, from 2003 to 2004, women's median annual earnings fell by 1 percent, for the second consecutive year-to-year decline. Yet the gender-wage ratio rose slightly, because men's earnings dropped even more, by 2.3 percent."<p>
At the same time, some women do also benefit from, or at least pander to, sexist notions. Playboy CEO Christie Hefner makes her salary off the sexual exploitation and objectification of other women. Anti-abortion talk show host Laura Ingraham keeps her radio markets hare by preaching sexism. Overcoming sexism would threaten their livelihoods.<p>
The point is that the capitalist class has an interest not only in the economic exploitation of workers but also in the social oppression of women. The two are connected. We cannot limit the fight against sexism to challenging individual acts of violence and harassment by men against women; we also must change the whole social system built on sexism.<br>
<b>John Osmand</b>, Ventura, Calif.<p>
