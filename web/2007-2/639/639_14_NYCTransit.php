<font face="Times New Roman, Times, serif" size="5"><b>Building a stronger union in NYC transit</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Dan Clemente, TWU Local 100</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 3, 2007 | Page 14</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Coming off the two recent deaths of transit workers, along with some near misses as trains almost killed more track workers, Transport Workers Union (TWU) Local 100, which represents workers at the city's Metropolitan Transit Authority (MTA), is still coming to grips with how we improve working conditions.<p>
On June 1, the MTA stripped mandatory dues deduction from paychecks. Now, employees will pay their monthly dues on a voluntary basis--leaving the TWU facing a perilous battle.<p>
The union isn't going down without a fight. Union reps have traveled to terminals and bus depots to sign up workers for easy and diverse payment methods, and so far, about half of the 34,000 workers represented by the TWU have signed up for dues payment.<p>
The MTA's move may be damaging for several reasons.<p>
First, this is part of the punishment that a Brooklyn judge levied against the union for its 2005 strike, and it sends a clear message that they will be repercussions for city employees for striking.<p>
Under the latest stipulation, the TWU was offered the return of dues deduction if we don't ever strike again--though the MTA aims to continue trying to take away benefits while getting as much production out of workers as possible.<p>
The MTA is pushing cost-cutting measures, like reducing token clerks in favor of machines. There was also an effort last year to replace conductors with OPTO (one person train operations) on some lines. Public outcry and safety issues beat back that attempt, but the MTA is still pursuing the idea.<p>
The MTA's attack could give us new opportunities to discuss how fractured the union is, and how we can now strategize for a worker-organized union. One key point of debate is Local 100 President Roger Toussaint, the former union dissident who won the local presidency, and then turned on the grassroots union activists who were the core of his campaign.<p>
Some colleagues have told me that as long as Toussaint is union leader, they won't pay dues. Their anger with him is understandable. He allowed the settlement of the 2005 strike that put a controversial 1.5 percent health care co-pay into our contract, and disciplinary actions, demotions and firings have since risen astronomically.<p>
But without dues, the union won't be able to defend workers from the MTA's attacks. No funds equals no personnel equals no resources equals no union! Where will my line of defense be if my union no longer exists?<p>
That's why I stepped up to pay my union dues, and I want the remaining transit workers to do the same. We must unite now!<p>

