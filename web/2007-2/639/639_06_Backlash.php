<font face="Times New Roman, Times, serif" size="5"><b>Violent consequences of a racist backlash</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 3, 2007 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">THE RACIST backlash against immigrants has had real consequences in a small town outside of Portland, Ore.<p>
On May 24, Edwin Alfonzo Gonzales and Alex Bivian Guzman, two Latino men in their late 20s, went to rest and relax at Wagon Wheel Park after their shift at a lumber mill in Molalla, Ore. They chatted with two girls, who were at the park with a large group of male teenagers.<p>
Gonzales and Guzman left the park to buy some beer, and when they returned, they were confronted by a mob of 20 to 30 white male teens. One of the teens threw a large rock at Gonzales and encouraged others to do the same. The mob surrounded Gonzales and Guzman, pushing them to the ground and chanting "Go back to Mexico" as they kicked the two men and beat them with rocks.<p>
"The poor souls were terrified," said detective Jim Strovink, spokesman for the Clackamas County Sheriff's Office, "They were minding their own business, and they were attacked with absolutely no provocation by strangers." Gonzales and Guzman tried to flee, making it back to Gonzales' car, but the car stalled while the crowd continued to throw rocks, smashing out the rear window.<p>
Molalla Police officers finally arrived on the scene. Clackamas County sheriff's deputies arrested Austin Wright Greenwood, who, according to Gonzales, instigated the attack. According to the deputy, Greenwood claimed, "The only thing I did was wrestle with some fat Mexican." After his arrest, Greenwood spat on the patrol car window and urinated in the back seat while he sat in custody.<p>
In a climate of anti-immigrant scapegoating, in which we are told that immigrants are to blame for everything from low wages to violent crime, it isn't surprising that two innocent men relaxing after a day at work would be attacked on the basis of their skin color.<p>
Law enforcement officers in Clackamas County claim the beating was an "isolated incident," but this crime is simply one that has been reported. There are undoubtedly countless others occurring on a daily basis.<p>
According to a recent study by the Southern Poverty Law Center, hate crimes against Latinos are on the rise in the U.S.<p>
Last year, the National Knights of the Ku Klux Klan held an anti-immigration rally that drew more than 300 Klansmen and Klan supporters. At the rally, robed Klansmen burned a 22-foot-high cross and yelled, "Let's get rid of the Mexicans!"<p>
The Klan, along with other neo-Nazi and white supremacist groups have allied themselves with the racist vigilantes of the Minuteman Project, whose voice politicians and the mainstream media have largely accepted as legitimate in the debate over federal immigration policy.<p>
The rise of the far right and the increase in hate crimes towards Latinos is fueled by the politicians' anti-immigrant crusade in Washington and the crackdown on Latino immigrant communities around the country by Immigration and Customs Enforcement (ICE).<p>
The months since Congress passed legislation authorizing the construction of a 700-mile, double-layered fence on the U.S.-Mexico border have been filled with military-style mass arrests, separation of small children from parents, arbitrary detention and rapid deportation carried out by ICE agents and aimed at terrorizing immigrant communities. The government has succeeded at instilling fear into immigrant communities and in doing so, has emboldened extremist groups like the KKK and the Minutemen.<p>
But the fight for immigrant rights is far from over. The widespread opposition to the bipartisan Hagel-Martinez immigration reform "compromise" and this year's larger-than-expected protests are evidence of the potential to mobilize in mass for immigrant rights.<p>
On May Day, more than 5,000 people marched in the streets of Portland, only 30 miles from where Gonzales and Guzman were beaten. The crowd shouted for amnesty and an end to the ICE raids and border militarization.<p>
Activists must counter the rise in hate crimes against Latinos with an increase in struggle against the racist bigots near home and in Washington. This attack has spurred into action a variety of anti-racist and pro-immigrant groups in the area. Hopefully, activists can draw more people into the arena of action- demanding amnesty for all and an end to the raids and deportations.<p>
It's time to stand up and say no to the violence occurring against our brothers and sisters. An injury to one is an injury to all!<br>
<b>Adam Sanchez and Andrea Hektor</b>, Portland, Ore.<p>

