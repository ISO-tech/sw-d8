<font face="Times New Roman, Times, serif" size="5"><b>Demanding care from the VA</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Chris Mobley and Erik Wallenberg</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 3, 2007 | Page 14</font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--Chanting "Fund the wounded not the war! What the hell is Congress for?" protesters led by Iraq Veterans Against the War members marched to the Department of Veterans Affairs (VA) Medical Center here July 28.<p>
More than 300 people turned out to demand more funding for the VA system across the country. Many veterans now feel the crunch of a lack of an adequate system. According to the California Nurses Association, "A returning soldier has to wait an average of 165 days for a VA decision on initial disability benefits, and an appeal can take up to three years." In the Seattle area, the backlog of cases is as high as 3,000 people.<p>
This burden on the VA system led the head of the VA, Jim Nicholson, to resign July 17. Yet the promise by politicians to help the ailing system has rung hollow.<p>
Activists from the Troops Home Now Coalition, Iraq Veterans Against the War, Veterans for Peace, the ISO and staff at the VA hospital decided to join forces to organize the protest. Afterward, a panel of speakers addressed the crowd at a nearby community center.<p>
"We have produced hundreds of thousands of psychiatric casualties," remarked Dr. Evan Kanter, who works as a psychiatrist at the VA. "We're still healing from Vietnam, and we're now beginning to reel from this war."<p>
Kanter, who trained in Baghdad before the first Gulf War, added that the Iraqi health care system "has been destroyed. This is the health care system that was the best in the Arab world two decades ago. It's gone, all gone."<p>
Another speaker, Iraq veteran Darrell Michaels, explained how the military tries to avoid paying for services by "blaming [soldiers for] substance abuse and pre-existing personality disorders," pointing out that 50,000 have been treated for PTSD already.<p>
Dr. Nada Elia of the National Council of Arab Americans added, "There is not PTSD in Iraq. There is no 'post.' It's daily." Elia's research among returning U.S. soldiers found that "47 percent of reported domestic violence in the military results in no action. It's not a few bad apples; it's systemic when the military trains in the glorification of violence."<p>
Dave Schop, a veteran active in the Vietnam soldiers' rebellion, recounted how soldiers organized locally in the VA and other hospitals in Seattle to fight for veterans; benefits and fight the racist practices of denying Black and Latino soldiers care. "If I've learned anything these past 35 years, it's that this for profit system--let's call it by its name, capitalism--can't provide for humanity," he said.<p>
Evan Knappenberger, a former Army intelligence analyst who protested the military's "stop-loss" policy, challenged the notion that al-Qaeda is a major force in Iraq. "Of the hundreds of prisoners I interrogated, two were foreign," he said.<p>
The struggle to rebuild a soldier's antiwar movement is forging ahead. The protest and panel discussion showed the potential for activists to organize around the urgent issue of veterans' health care.<p>

