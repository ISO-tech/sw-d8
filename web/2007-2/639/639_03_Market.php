<font face="Times New Roman, Times, serif" size="5"><b>Behind the stock market plunge</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 3, 2007 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>LEE SUSTAR reports on the potential of a credit crunch hitting the U.S. economy.</b><p>

THE STOCK market dive in late July signaled that policies to make borrowing easier which boosted the world economy may soon give way to a credit crunch that could squeeze off growth.<p>
The stock market stabilized when it reopened the following week. But like the last market plunge in late February, the big drop in the main Dow Jones industrial average at the end of July highlighted fears that the housing slump in the U.S. was having a knock-on effect on the financial sector that could choke off credit to both consumers and big business.<p>
Tellingly, big corporate takeovers--such as the private company Cerberus' $7.4 billion buyout of Chrysler--had to be delayed when loans suddenly weren't available to finance them.<p>
Even more remarkable is the fact that the stock market dive came amid news that the U.S. economy expanded at a 3.4 percent annual pace in the second quarter of 2007--a big jump from the paltry 0.6 percent growth rate charted for the first quarter of the year.<p>
But instead of the news pushing up stock prices, the prospect of a credit crunch spurred panic selling on Wall Street, with the Dow Jones average ending down 4.2 percent for the week.<p>
The credit squeeze meant that Wall Street banks and huge funds of private capital, known as private equity firms, were having a harder time finding buyers for bonds to finance big corporate takeovers.<p>
"Rather than negotiating new deals to take public companies private with billions in easy and cheap credit, many bankers and private equity firms spent the weekend trying to figure out what to do with dozens of pending deals that are now faced with the higher cost of debt," the <i>New York Times </i>reported after the stock markets closed way down July 27.<p>
As financial writer Joe Gose noted in <i>Real Estate Investor </i>magazine, a combination of rising interest rates, tougher conditions demanded by lenders and the impact of unpaid mortgages together "signal the end of a frothy period that might best be described as lenders gone wild."<p>
A great part of this "wildness" was making home loans to people whose credit histories didn't qualify them for traditional mortgages. So-called "sub-prime" borrowers now find themselves hit by increased interest rates on adjustable rate mortgages, which make payments higher.<p>
The resulting foreclosures and late payments have rippled through the financial sector and wider economy. The reason: Sub-prime loans were repackaged as securities and even more complex financial instruments known as collateralized debt obligations (CDOs). Both types of securities were bought and sold by Wall Street firms--including the big, unregulated investment pools known as hedge funds.<p>
Now, CDOs tied to sub-prime loans are going bad as foreclosures rise. The big Wall Street firm Bear Stearns had to shut down two hedge funds after they lost virtually all of their value because of investments in CDOs.<p>
That's just one example of how the housing slump is affecting the wider economy. As <i>Wall Street Journal </i>columnist Joe Patterson explained: "Mortgage lenders are clamping down on risky loans after a jump in sub-prime mortgage defaults. Banks have started calling in collateral from hedge funds that dabbled in exotic securities linked to sub-prime loans.<p>
"Meantime, investors are thumbing their noses at banks trying to sell them loans or junk [high-interest] bonds used by private-equity firms to finance leveraged buyouts. It all means less money for investment."<p>
If the higher-than-expected GDP figures for the U.S. economy didn't counteract the gloom in the stock market, a closer look at the numbers explains why.<p>
A big part of the growth in the second quarter came from government expenditures, including a 9.5 percent increase in defense spending. Investment in plant and equipment, by contrast, grew at only a 2.3 percent pace--reflecting the continued reluctance of Corporate America to expand production despite the economic recovery.<p>
Another source of worry is the fact that personal consumption increased only 1.3 percent in the second quarter, down from 3.7 percent in the first three months of 2007.<p>
There's no telling when these accumulating problems will lead to recession. Unemployment remains low at 4.5 percent, and the world economy continues to have its biggest period of growth in 30 years.<p>
But the problems of the U.S. economy, the world's biggest, will eventually take their toll.<p>

