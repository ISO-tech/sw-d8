<font face="Times New Roman, Times, serif" size="5"><b>Wal-Mart--The hilarious new musical</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Meghan Behrent</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 28, 2007 | Page 11</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i><a href="http://www.walmartopia.com" target="_blank">Wal-Martopia</a>, </b></i>music and lyrics by Catherine Capellaro and Andrew Rohn, now at the Minetta Lane Theater, New York City.</font><p>

<font face="Times New Roman, Times, serif" size="3"><i>WALMARTOPIA</i>, THE new off-Broadway musical, is a welcome breath of fresh air from the Disney-dominated world of Broadway musicals.<p>
Described as a "an irreverent political satire of big business and eternal smiley faces," <i>Wal-Martopia</i> is a hilarious musical that takes aim at corporate greed, the Wal-Martization of America and the war on terror. It provides a moving testament to the creativity and aspirations of working people--all the while keeping the audience laughing so hard it hurts.<p>
The first part of the musical is set in Madison, Wisconsin, and tells the story of Vicki Latrell played by the amazing Cheryl Freeman, a single mother who after five years of working at Wal-Mart and hoping for a promotion is still on welfare.<p>
 Vicki still clings to the idea that if she works hard and follows every ridiculous demeaning rule, she might make manager and see an improvement in her life. Her teenage daughter Maia, who also works for Wal-Mart, disagrees.<p>
In one of the most powerful early musical numbers, "American Dream," Maia tells her mother, "Instead of a living, they give you a promise," and argues that only organizing a union can provide a dream "for the many not for the few."<p>
The aspirations of ordinary people are in stark contrast to the profit-driven Wal-Mart bosses who show complete disdain for their employees as they proudly celebrate union-busting.<p>
In the "March of the Executives," Wal-Mart CEO Scott "Scooter" Smiley explains Wal-Mart's success: "We're big. We're really big." He lauds the company's anti-union policies and trains his board of directors in threatening a factory in China with taking their business away if they can't find younger child labor to cut costs.<p>
Meanwhile, Smiley plots to further expand Wal-Mart's influence over the world--in the process, they bring back the disembodied head of Sam Walton, who explains "The meek may inherit the world, so I don't have to give anyone anything now."<p>
Meanwhile, after being passed up yet again for a promotion, Vicki finally listens to her daughter and decides to organize and speak her mind, lambasting the Wal-Mart board of directors in the style of a modern Norma Rae.<p>
Unfortunately, as a result, she is thrust 30 years into the future in an Orwellian world where Wal-Mart has taken over America installing med-marts, prison-marts and school-marts where children learn how to shop. In this nightmarish Wal-Martopia, resistance has not been altogether quelled, however.<p>
We learn that Vermont--after declaring the entire state a historical landmark to prevent Wal-Mart's expansion--has seceded. National Security Mart is charged with fighting a war against insurgent terrorists in Vermont.<p>
To convince a war-weary population of the necessity of "liberating" Vermont from insurgents, Smiley hires WalArts to perform a musical celebrating the war effort with a raucous musical number entitled "These Bullets Are Freedom."<p>
Despite the nightmarish vision of a future Wal-Martopia, a strength of the musical is the way it gives voice to the real struggle and resistance of working people trying to maintain their humanity in a world that systematically dehumanizes them.<p>
In the poignant song "What Kind of Mother," Vicki debates whether to fight back and take a stand against corporate greed, weighing her responsibility to keep her daughter safe against her desire to be a role model and to earn her respect by fighting for justice. Vicki asks herself, "How's a mother supposed to choose when she has so much to lose?"<p>
As you leave the theater, passing through halls with articles about Wal-Mart's real-life abuses and a large poster with the words, "Join the Revolution," you can't help but want to heed that call.<p>

