<font face="Times New Roman, Times, serif" size="5"><b>Battle in Illinois over new abortion clinic</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 28, 2007 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">A CITY about 35 miles west of Chicago has become ground zero in the fight over women's access to abortion and other reproductive health care, as Planned Parenthood faces right-wing opposition to the opening of a new facility.<p>
Abortion rights opponents won a victory in court September 20 when a federal judge delayed the opening of Planned Parenthood's clinic in Aurora, Ill., on the grounds that the group may have violated regulations when it applied for building permits under the name of a subsidiary, Gemini Office Development.<p>
The clinic was supposed to open September 18, but cannot until Aurora city officials finish their review and provide occupancy permits. City officials claim they should have had more information about what kind of business was going to open on the site, but Planned Parenthood officials say that they followed the letter of the law.<p>
Local Planned Parenthood director Steve Trombley told reporters that the organization hoped using the alternative name might keep anti-abortion picketers away, but it never tried to hide the purpose of the building.<p>
The real issue is that Aurora is discriminating against the facility because it is an abortion provider, and the city is crumbling under the pressure of anti-abortionists. "Once the political firestorm started, we were singled out for differential treatment," argued Planned Parenthood attorney Christopher Wilson in court.<p>
Anti-choice forces have targeted the site of the coming clinic. Eric Scheidler, son of grizzled anti-choice activist Joseph Scheidler, and the Pro-Life Action League have made the clinic site the focus of their protests. Earlier this month, hundreds gathered in front of the site of the new clinic, one of several demonstrations organized since news of the opening came out.<p>
When they heard the news that the new clinic couldn't yet open, anti-abortion activists were overjoyed. "We're having a big party," Joe Scheidler told reporters. "We're learning from each other what really bothers Planned Parenthood. Some of the things that have worked here will be tried around the nation."<p>
The Planned Parenthood clinic would be a welcome resource for women in the area. Statistics from the Centers for Disease Control rank Illinois the sixth highest for reported AIDS cases among the 50 states. In 2000, Illinois ranked third in the number of cases of gonorrhea infections. Adolescents aged 10 to 19 made up 39 percent of those reported cases.<p>
If it is allowed to open, the 22,000-square-foot, state-of-the-art clinic would provide abortions--making it one of only two Planned Parenthood facilities in the Chicago area to do so--as well as birth control, cancer screenings and gynecological exams.<p>
The right wing hopes to use the Aurora clinic as a lightning rod to further its anti-choice crusade, but for women in Illinois, the possibility of a new area clinic could mean the difference between life or death. If we are going to push back the right wing, the pro-choice side needs to start thinking about mobilizing protest as seriously as the right does.<p>

