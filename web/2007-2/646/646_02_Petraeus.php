<font face="Times New Roman, Times, serif" size="5"><b>Don't they have anything better to do?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 28, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">WITH THE crisis of the U.S. occupation of Iraq growing ever-more desperate--by every measure, from Iraqi casualties to U.S. combat deaths to worsening ethnic cleansing--the military official most responsible for planning and executing Bush's "surge" of combat troops shows up to testify to Congress.<p>
You'd think it would be the perfect opportunity for the Democrats to blast the Bush administration for making a catastrophic situation even worse--and finally start using their majority in Congress to do something against the war.<p>
Think again. Gen. David Petraeus was given a hero's welcome in the congressional hearing rooms earlier this month. Even the most liberal Democrats gushed with fawning stories about their meetings with the general--a stark contrast to his commanding officer, Admiral William Fallon, who reportedly told Petraeus, to his face at their first meeting earlier this year, that he was a "sycophant" and "ass-kissing little chickenshit."<p>
After Petraeus' testimony, the Democratic leadership in the Senate did allow one timid proposal--to increase the time troops spend back home in the U.S. to match the 15 months they're deployed in Iraq, hardly a radical proposal for withdrawal--to come to a vote.<p>
But the Democrats knew full well that the Republicans would use a parliamentary maneuver to block the measure with a minority of 40 votes--which these days seems to be enough to stop the "world's greatest deliberative body" from passing anything not to the liking of the White House.<p>
Instead of taking action on the occupation, senators spent their time criticizing not Petraeus or Bush...but the liberal peace group MoveOn.org, for the crime of running an ad in the <i>New York Times, </i>headlined "General Petraeus or General Betray Us?"<p>
The whole sorry spectacle led to the Democratic-controlled Senate passing a resolution--by an overwhelming 72-25 margin--that expressed "full support" for Petraeus, and condemned MoveOn for "personal attacks on the honor and integrity of General Petraeus and all members of the United States Armed Forces."<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">BUSH AND the Republicans seized on the MoveOn ad in typical fashion to deflect attention from the substance of the debate. "I felt like the ad was an attack not only on Gen. Petraeus, but on the U.S. military," Bush said at a press conference, concluding that the Democrats must be "more afraid of irritating [MoveOn] than they are of irritating the United States military."<p>
That's not fair at all. The Democrats are obviously far more scared of being seen as critics of the U.S. military--including its commander in chief, in most cases.<p>
Their behavior during the MoveOn ad controversy proves it. Nearly half of Senate Democrats supported the anti-MoveOn resolution, and every one of those who voted against it--Hillary Clinton, Christopher Dodd and Barbara Boxer, among them--voted for a barely distinguishable alternative measure that didn't name MoveOn, but condemned its ad as an "unwarranted personal attack" on Petraeus.<p>
The biggest irony of all is that if any actor in this farce has a history of being afraid of irritating one of the others, it is MoveOn that has been frightened of angering Democrats.<p>
The liberal Internet network has always been dedicated to the most conservative possible opposition to the war--and, in Cindy Sheehan's words, "support[ing] Democrats at the expense of democracy." In the wake of the 2004 election, MoveOn even "moved on" from calling for U.S. withdrawal from Iraq--on the grounds of political "realism."<p>
If the Democrats ever deserved the hopes placed in them by the millions who voted against the war in the 2006 election, they would be taking action to get U.S. troops out of Iraq now. The fact that they aren't underlines the need to build an antiwar movement independent of both mainstream parties.<p>

