<font face="Times New Roman, Times, serif" size="5"><b>Workers fight no-match firings at Ballco</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 28, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">AURORA, Ill.--Workers in the Chicago area are once again fighting management attempts to fire immigrant workers for irregularities in their Social Security numbers.<p>
Managers at the Ballco Manufacturing Co. ball bearing plant in this city west of Chicago fired eight workers September 21 because their Social Security numbers allegedly don't correspond those on file with the government.<p>
One of the workers, Israel, had earlier contacted a new hotline established by the United Electrical workers' union to support workers threatened with termination based on "no match" letters from the Social Security Administration.<p>
Management acted first by firing the eight workers. When 15 more workers walked off the job in solidarity, they were fired, too. Several others decided to walk the picket line as well. All are Mexican immigrants.<p>
When UE organizers learned of the firings, they immediately set up a picket line. By the third day, some 35 workers--about a third of the workforce--were out.<p>
The struggle at Ballco comes in the wake of the strike at Cygnus Corp. in South Chicago, where nonunion workers walked the picket line for two weeks until management withdrew its threat to use no-match letter to terminate workers.<p>
Immigrant rights and labor organizer Jorge M&uacute;jica, who advised the Cygnus workers, now runs the no-match hotline for UE and is coordinating the union's activities at Ballco. "Cygnus was the decisive thing" in setting up the hotline that is putting the union in touch with immigrant workers with similar issues, he said.<p>
The threat of being fired for no-match letters looms over millions of immigrant workers, owing to regulations by the Bush administration that would use the Social Security database as an indicator of immigration status. A federal judge has imposed an injunction on these new rules, but employers are using no-match letters to fire workers anyway, as management at Cygnus attempted to do.<p>
Mark Meinster, an international representative with UE, pointed out that there are differences between Cygnus, where almost all the workers walked out, and Ballco, where a majority remain on the job. "This is a tough fight," he said. "But the message is that when workers receive no-match letters, they can contact us and organize."<p>
The company is vulnerable. A maker of precision ball bearings for heavy manufacturing equipment, Ballco's customers include FMC, the huge chemical products company, and Flowserve, a leading make of industrial valves, seals and pumps. According to Marcos, one of the fired workers, it will take months for replacement workers to learn how to properly operate Ballco's production machinery.<p>
Workers did offer to return to work unconditionally in order to organize on the inside, but management refused. So they're stepping up the fight--and asking for solidarity.<p>
<i>To support the strike, visit the picket line at 2375 Liberty St. in Aurora, Ill. Donations can be sent c/o UE District 11, 37 S. Ashland Ave., Chicago, IL 60607, Attn. Jorge M&uacute;jica. Workers with Social Security no-match issues can call 1-888-DIGNIDAD</i>.<p>

