<font face="Times New Roman, Times, serif" size="5"><b>UMass students put pressure on administration</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By James Fiorentino (GEO/UAW 2323) and Rachel Wilsey</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 30, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">AMHERST, Mass.--Over 600 students at the University of Massachusetts-Amherst (UMass) participated in a rally to kick off a two-day "general student strike" on November 15 called by the Student Government Association in conjunction with the Graduate Student Senate.<p>
Organizers' demands include: a rollback of skyrocketing student fees, a commitment by UMass to honor its pledge to increase diversity, an end to plainclothes police patrols in dormitories and restoration of student control over the Campus Center-Student Union complex.<p>
Speakers at the rally--most of them students of color and women--condemned the administration's shift toward privatization and its abysmal record on affirmative action. The population of Massachusetts is 7 percent African American and 8 percent Latino. Latinos account for just 3 percent of the general student population, and African Americans account for less than 3 percent.<p>
Working-class students are being priced out of attending UMass. In 2001, in-state tuition (including fees and room and board) was $10,822. In 2007, it was $17,399.<p>
Student organizers led a 300-strong crowd into the Whitmore Administration building, where they sat-in until administrators agreed to a private meeting with interim chancellor Thomas Cole on November 20. While the rally and the occupation were a positive step forward, the "strike" was organized by a small group without giving the larger student body an opportunity to shape the action.<p>
The Graduate Employee Organization (GEO/UAW 2322), which is battling for a contract, played almost no role in organizing, and many students were confused about who called the action and whether it was sanctioned by GEO. If students and graduate employees hope to win their demands, a movement needs to be built from the bottom up that welcomes all students' participation.<p>
The current organizing is building on the momentum of protests last spring, when thousands protested UMass' decision to give former White House Chief of Staff Andrew Card an honorary Ph.D. Through this process, activist groups recognized the need to work together to win larger campus-wide demands.<p>
Student activists today can learn from the rich history of struggle at UMass. In 1991, undergraduates united with graduate employees in a campus-wide general strike to force UMass administrators to recognize collective bargaining rights for graduate students.<p>
Thousands participated in a 15-day occupation and shutdown of key campus buildings and organized daily mass meetings. This is the kind of mass student action that can win real gains from the administration.<p>

