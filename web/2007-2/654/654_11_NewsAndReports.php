<a name="Seattle"></a><font face="Times New Roman, Times, serif" size="4"><b>Seattle students' antiwar walkout</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jason Farbman</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--More than 500 students at high schools and colleges throughout Seattle walked out of class November 16, in protest of the occupation of Iraq. The event was organized by Youth Against War and Racism.<p>
At Seattle Central Community College (SCCC), 50 people marched through halls, banging on walls and chanting "Walk out! Stop the war!"<p>
There was a brief speak-out, where campus organizers from the Antiwar Collective and Iraq Veterans Against the War (IVAW), among others, addressed the crowd. IVAW member Mateo Rabecchi took the Democrats to task for failing to honor the clear antiwar mandate they were elected on a year ago. To applause, he said, "When I was in the military, if we were given a job, and didn't do it, there were consequences."<p>
The SCCC students marched downtown, where they were met by hundreds of other students. After a rally, students and their supporters marched to a recruiting station in the Central District, a working-class neighborhood. As the recruiting station was surrounded by protesters, it was barricaded off by police, in effect shutting operations down for the day.<p>
Originally, police had refused to issue a permit for any march leading past the recruiting station. Eventually they backed down, as organizers insisted on exercising their rights.<p>
Students from other cities in Washington walked out as well that day, including as many as 400 in Olympia and 100 in Bellingham. The day before, nearly 100 students walked out in Tacoma.<p>
Teachers sympathetic to the walkout were punished at one school, in Tukwila, where 200 students walked out. One teacher was put on administrative leave, and five others received memos indicating they were under investigation for "misconduct relating to you in connection with the student walk-out."<p>
While school administrators' efforts may be trying to intimidate some students now, it is clear that many are standing firm in their resolve to see a new student antiwar movement emerge.<p>
