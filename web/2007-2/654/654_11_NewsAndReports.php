<p><a name="Resisters"></a><font face="Times New Roman, Times, serif" size="4"><b>Support war resisters</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ANTIWAR ACTIVISTS in Canada and the U.S. are protesting a Canadian Supreme Court decision not to hear the appeals of two U.S. soldiers who sought refuge in Canada rather than fight the U.S. occupation of Iraq.<p>
The court denied the appeals of Jeremy Hinzman and Brandon Hughey, who "were among the first Iraq War resisters to arrive in Canada in 2004," wrote Jeff Paterson, a resister during the first Gulf War and leading member of the group Courage to Resist.<p>
"Their case for political refugee status rested on the fact that the U.S. invasion and occupation is illegal under both U.S. and international law. Based on the United Nations convention on refugees, they should not be prosecuted for refusing to fight in an illegal conflict. Canada's Immigration Review Board and the Canadian courts, however, denied a hearing on these facts."<p>
<i>Courage to Resist is calling on activists <a href="http://www.couragetoresist.org/x/index.php?option=com_content&amp;task=view&amp;id=499" target="_blank">to put their names to an appeal</a> to Canadian officials on behalf of the resisters.</i><p>

