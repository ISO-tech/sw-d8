<p><a name="Health"></a><font face="Times New Roman, Times, serif" size="4"><b>Healthcare-Now! conference</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Helen Redmond</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--One hundred people representing 30 states attended a national strategy conference held here over the weekend of November 10-11, sponsored by the group Healthcare-Now!<p>
Leading members of the National Nurses Organizing Committee, California Nurses Association (CNA), Physicians for a National Health Plan, the United Steel Workers, United Electrical Workers, United Auto Workers and dozens of other grassroots organizations participated, with the aim of building the movement for a single-payer health care system.<p>
The conference was inspiring for the sense of optimism and rock-solid commitment to single-payer that pervaded.<p>
Rep. John Conyers (D-Mich.) opened the meeting by discussing HR 676, legislation for a national single-payer health care system that he is sponsoring. So far, 86 members of Congress have cosponsored the bill.<p>
Speakers talked about health care as a social justice issue, how building a grassroots movement is crucial, and how many politicians will have to be forced to support single-payer because they accept money from health insurance corporations.<p>
Activists gave examples of actions across the country they are using to build the fight, including "truth hearings" (meetings where people tell health care horror stories); organizing a picket in front of a Blue Cross building; and launching the <i>Sicko</i>-Cure Road Show, which will visit 11 states in 22 congressional districts. There also were breakout sessions that focused on organizing among the labor, faith, and other communities.<p>
Plans for the upcoming year include a national day of action against the insurance companies. The fight for single-payer is on!<p>

