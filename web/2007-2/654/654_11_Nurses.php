<font face="Times New Roman, Times, serif" size="3">Sarah Hunley, a 37-year veteran RN who worked at the Harlan hospital, says, "We're being asked to do an impossible task, to be responsible for too many patients. Some days, we have as many as 12 patients to care for. That's too much for one person to do without making a mistake."<p>
Harlan County is no stranger to militant labor struggles. It's known for the 1973 miner's strike, which is retold in the award-winning documentary film <i>Harlan County, U.S.A</i>. It's also where ARH now has its corporate headquarters. In fact, all of these hospitals were opened in 1956 by the United Mine Workers of America (UWMA) union and its then-President John L. Lewis to help sick miners.<p>
As soon as the RNs walked out, the company brought in 150 replacement nurses or "scabs," with some being housed in vacant wings of the hospitals. UMWA President Cecil Roberts condemned these practices.<p>
ARH has also hired private security and used video cameras to try to intimidate picketers and their supporters. Recently, a union official's car was burned just minutes after he got off the picket line in West Virginia. When he tried to report this to the police, he was told they were closed, and no detective was available.<p>
Solidarity has been impressive. The United Steelworkers (USW), which represents certified nursing assistants, licensed practical nurses (LPNs), housekeepers, and maintenance and clerical workers, supports the nurses. The 2,700 USW members also struck against ARH for three weeks this past spring.<p>
The communities served by the nurses are also giving them strong support. The Letcher (Ky.) Fiscal Court (equivalent to a county council) voted unanimously for a resolution supporting the nurses.<p>
In mid-November, a caravan of nurses traveled across the country to walk the picket lines, including members of the California Nurses Association/National Nurse Organizing Committee (CNA/NNOC), and the Ohio, New York and Washington Nurses Associations.<p>
As Hunley states, "We're fighting a big corporate giant, but we are right in what we are doing."<p>

