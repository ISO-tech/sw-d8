<font face="Times New Roman, Times, serif" size="5"><b>Nurses' battle against understaffing</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 30, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>ELIZABETH LALASZ reports on a strike by nurses against hospitals in Kentucky and West Virginia.</b><p>

NEARLY 700 registered nurses have been on strike at nine Appalachian Regional Healthcare (ARH) facilities since October 1. The strike has affected hospitals in Beckley and Hinton, W.V., plus seven hospitals in Kentucky, including Harlan County.<p>
The nurses, members of the United American Nurses (UAN), are organizing over understaffing and rampant mandatory overtime, which prevent them from giving good quality care to their patients.<p>
The proposed contract rejected by the nurses would have increased insurance premiums for families, eliminated a policy of giving nurses 40 hours of pay for 36 hours of work, and reduced holiday pay from double-time to time-and-a-half.<p>
But the strikers say the dispute isn't about salaries. They say staffing levels were spread too thin, and mandatory overtime had become the norm.<p>

