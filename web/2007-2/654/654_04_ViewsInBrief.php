<p><a name="Racism"></a><font face="Times New Roman, Times, serif" size="4"><b>The racism of immigration laws</b></font><p>

<font face="Times New Roman, Times, serif" size="3">YOU COULDN'T have been more right in the article "Who benefits from immigration bans?" (March 31, 2006).<p>
The United States has a history of pulling in immigrants when it needs them and then kicking them out when it doesn't. We did it with the Chinese as railroad labor, and then attempted to get rid of them with the Chinese Exclusion Acts. We did it with the Japanese and many other groups.<p>
Now we are doing it to the Hispanic population. Regardless as to how Hispanic immigrants have arrived, they have become a part of American society. They have worked, paid taxes and otherwise played by the rules; therefore, they shouldn't be denied the same opportunities that the Europeans took advantage of so long ago.<p>
We have to keep in mind that these are people with families, jobs and something to contribute to American society. They are not dogs that we have to keep locked up behind a wall, but are like every other human being who is desperate to get out of poverty. They are not asking for a handout, but merely any opportunity for a better life for their family.<p>
It's also important to distinguish between illegal immigration, border security and the fight on terrorism. Securing the national border would include securing <i>every</i> point of entry: our shores, seaports, land ports, airports and the Northern and Southern borders. Securing the <i>Southern</i> border is not securing our <i>national</i> border--it's stopping Mexicans from coming across.<p>
Safeguarding American soil has nothing to do with stopping Mexicans from entering the country. Mexicans did not attack the World Trade Center, nor did they bomb the transit systems in Spain or London. There are no incidents of Mexicans engaging in terrorism in the U.S. The issue is that Americans do not want to see Mexicans in this country. It's a racially divisive issue.<p>
I have a problem with the way the legal and undocumented immigrants are treated in this country. Legal immigrants are treated as if they were undocumented, and the undocumented are treated as dogs, sometimes locked in deportations facilities for months, and sometimes years.<p>
Americans should do their research on the contributions that immigrants bring to America before they take action to discriminate, oppress, and/or deport immigrants. Thanks for that article. It's nice to know someone has their head on straight.<br>
<b>Trisha Garcia</b>, from the Internet<p>
