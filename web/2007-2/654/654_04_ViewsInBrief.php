<p><a name="Right"></a><font face="Times New Roman, Times, serif" size="4"><b>When the right likes free speech</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I AM not the least bit shocked at the indifference displayed by the government toward the Jena 6. There is a long legacy of reluctance to confront racism head-on through the enforcement of consequences, namely hate crimes laws.<p>
In <i>R.A.V. vs. City of St. Paul, Minnesota</i> (1992) the Court ruled that St. Paul's hate crimes ordinance was too narrow and essentially upheld the right of free expression of a racist to burn a cross in the fenced yard of an African American family. Justice Scalia argued that the ordinance seemed to only be concerned about bias expressed toward "race, color, creed, religion or gender" being "unfair" and "selective" toward other possible forms of bias.<p>
Well duh! What other major forms of hate crimes are there that the ordinance could possibly miss?! We all know that bigots typically use symbols to deliberately intimidate minority communities--that's the whole point--and that the symbols usually relate to one of the categories mentioned above.<p>
Yet we create a fighting words exception in 1942 for a man who yelled at the police "goddamned fascist" in the <i>Chaplinsky</i> case. In that case, we don't extend free speech to someone who challenges authority. Them's fighting words!<p>
However, in 1969, the Court ruled that a Klan leader could not be sanctioned for calling for violence to be used against Blacks and Jews, unless he showed "imminent lawless action."<p>
I just find it interesting that right wingers are suddenly enamored of the First Amendment when it comes to protecting the right to aim racist or sexist or homophobic slurs at minority populations, but want to invoke "fighting words" when antiwar protesters make statements or when citizens challenge the sanctity of the police. So Jena doesn't shock me one bit.<br>
<b>Faith Agostinone-Wilson</b>, Aurora, Ill.<p>

