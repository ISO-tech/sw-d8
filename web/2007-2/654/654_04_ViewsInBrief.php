<a name="Tacoma"></a><font face="Times New Roman, Times, serif" size="4"><b>Cracking down on Tacoma protests</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I RECENTLY witnessed how cops will go to any length in order to break up a peaceful protest.<p>
Tacoma, Wash., has the largest Homeland Security Detention Center in the Northwest, currently holding around 1,000 victims of Immigration and Customs Enforcement (ICE) raids. Recently, about 300 detainees came down ill from food they were served at the center.<p>
On November 10, I joined about 30 other demonstrators in downtown Tacoma to protest the detention center and the ICE raids that fill it. After an initial rally, we started marching to the center. Tom McCarthy, one of the demonstrators, was leading chants with a bullhorn.<p>
I was walking right next to Tom, discussing the next chant to use, when all of a sudden two cops grabbed each of Tom's arms and handcuffed him with plastic straps. I tried to take the bullhorn, but a cop grabbed it back. At the same time, the march was surrounded by other cops using motorcycles, cars and bikes.<p>
The surrounding cops held us with nightsticks while they summoned a paddy wagon and hauled Tom away in it. When I complained, a cop just told me, "He broke the law."<p>
At about the same time this was happening, 30 miles down the road in Olympia, cops were pepper-spraying other protesters who were trying to stop military equipment from being shipped through the Olympia port.<p>
It seems evident that cops will use any pretext to break up a protest they don't like. We have to oppose them by building protests that are even larger and louder.<br>
<b>Brian Huseby</b>, Tumwater, Wash.<p>
