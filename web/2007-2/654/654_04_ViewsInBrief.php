<p><a name="Congress"></a><font face="Times New Roman, Times, serif" size="4"><b>Congress ignores genocide</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IT IS a shame that congressional Democrats backed out of voting on a resolution condemning the genocidal slaughter of well over a million Armenians by Ottoman Turks in the early 1900s. Many people (and 20 countries) consider those past actions to be genocide. The Turkish government, which has done terrible things to the Kurds as well, doesn't.<p>
Meanwhile, in Iraq, the man known as "Chemical Ali" is about to be executed. He was found guilty of genocide and crimes against humanity because of his participation in the deaths of around 100,000 Kurds.<p>
I guess genocide is in the eyes of the beholder.<br>
<b>Chuck Mann</b>, Greensboro, N.C.<p>
