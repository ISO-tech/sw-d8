<font face="Times New Roman, Times, serif" size="5"><b>Killed by NYC police for holding a hairbrush</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Dave Florey</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 30, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">KHIEL COPPIN was Black, just 18 years old and "armed" with only a hairbrush--and on November 13, he died in a hail of 20 bullets fired by five men who are supposed to "serve and protect."<p>
The police shooting of Coppin occurred in the Bedford-Stuyvesant neighborhood of Brooklyn, five blocks from where Timothy Stansbury Jr., an unarmed, 19-year-old African American, was shot dead by police in January 2004 as he pushed open the door to his rooftop.<p>
Angry residents gathered at the site of the shooting moments after it took place. Civil rights leader Rev. Al Sharpton was among the more than 200 people who attended Coppin's funeral, and on November 17, some 1,000 people turned out for an anti-police violence demonstration in Hempstead on Long Island.<p>
Meanwhile, the media closed ranks around New York City Police Commissioner Ray Kelly's version of events in Coppin's death.<p>
The <i>New York Daily News, </i>for example, initially ran an article that quoted both an officer who called the shooting a "suicide by cop" and 17-year-old witness Andre Sanchez who says he saw Coppin drop the hairbrush and begin to raise his arms before police opened fire. But the next day, the <i>News'</i> editorial, titled "A Danger to Himself," stated there was "overwhelming evidence" police had reason to believe Coppin was a "gun-wielding, disturbed teenager."<p>
The <i>New York Times</i> followed a similar trajectory, printing Kelly's version of events and a separate blame-the-victim article titled "Before Shooting in Brooklyn, a History of Mental Illness and Family Discord."<p>
But many residents of Bedford-Stuyvesant--whose voices were mostly absent from media reports--are fed up with a police force that claims shooting an unarmed man is "within department guidelines."<p>
After Stansbury's killing in 2004, Kelly said there "appeared to be no justification" for the shooting and vowed things would change in his department after "an in-depth look at our tactics and training." But residents haven't seen behavior that would indicate a reevaluation of either tactics or training.<p>
"Shootings are still happening, too many times [against people] with no weapons," explained Tyreen Wright. "There were other killings of mentally ill people," Wright continued, referring, as an example, to another police shooting in East New York a few days after Coppin's death. "They should have mental health workers--hospitals--come out to deal with these people, not police."<p>
"A lot of these cops around here are just out of high school, and they're so quick to get nervous," said 24-year-old resident Jonathan Booker. "The law used to be you shoot in the legs or the arms--you disarm--but they don't do that anymore. They shoot to kill.<p>
"They've told three different stories. The commissioner said he pointed the brush like a gun, and they said it dropped after he was shot. But then they said they found it in his hoodie. How would they find it in his hoodie if he dropped it?"<p>
Another resident, Reginald Simpkins, said "nothing" had changed since the Stansbury shooting. "If I shot a policeman, I get 100 years," Simpkins said. Yet Officer Richard Neri Jr., who shot Stansbury "accidently," has gotten off scot-free.<p>
"They just violate your rights," said another young resident of the neighborhood. "They want to search us, and I'm right in front of my building," he said. He said that police routinely attempt to provoke him and his friends with questions like "Got a gun, buddy?"<p>
As intent as the media echo chamber seems to be in trying to convince people that the Coppin shooting was justified, many New Yorkers have too much experience with the NYPD to give officers the benefit of the doubt.<p>

