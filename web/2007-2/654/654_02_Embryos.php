<font face="Times New Roman, Times, serif" size="5"><b>Giving equal rights to frozen embryos?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 30, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THOSE SEEKING to restrict a woman's right to choose abortion are trying a new tactic: give the legal rights belonging to people to fertilized eggs.<p>
Recently in Colorado, the state Supreme Court cleared the way for anti-choice activists to collect signatures for a referendum proposed for the 2008 ballot. If the group Colorado for Equal Rights gets enough signatures, the referendum would allow voters to amend the state constitution to "include any human being from the moment of fertilization as a 'person'...in those provisions of the Colorado Constitution relating to inalienable rights, equality of justice and due process of law."<p>
Planned Parenthood of the Rocky Mountains Regional President Vicki Cowart told <i>Time</i> magazine that the measure would outlaw all abortions--presumably even in cases where the health or life of the woman is at stake. Moreover, Cowart said, "[b]y defining 'person' as any fertilized egg, the measure would call into question the legality of most hormonal birth control methods, such as birth control pills, as well as in-vitro fertilization."<p>
Embryos created for fertility treatments, but not used, could not be disposed of or donated for scientific research legally, even at the request of biological parents.<p>
Colorado isn't the only state where anti-choice groups are following this tack. Groups in Michigan, Mississippi and Montana are attempting to get similar laws passed, and the Georgia General Assembly is considering a proposal to put a similar constitutional amendment on the state's 2008 ballot. In all, a dozen states are considering similar measures.<p>
Anti-choice activists claim these laws are about protecting the "life" and "personhood" of a fetus. In reality, they would <i>take away </i>the "personhood" of women--by reducing a woman to the equivalent of a walking uterus and potentially making it illegal for her to take any action (like the use of an intra-uterine device or oral contraceptives) that might interfere with the ability of a fertilized egg to implant itself.<p>
Attempts to pass such laws are one more step in the right wing's attempt to overturn the 1973 <i>Roe v. Wade</i> decision giving women the right to choose abortion.<p>
While <i>Roe</i> remains the law in name, the right's strategy of pushing through one restriction after another has meant that an abortion has become increasing difficult to obtain. Today, 87 percent of U.S. counties have no abortion provider.<p>
Despite the right's success in restricting abortion rights, however, a poll released in October by the Center for Reproductive Rights showed that 55 percent of respondents wanted abortion rights to be protected by federal law.<p>
We need to build on this support to renew the fight for abortion rights--without apology.<p>

