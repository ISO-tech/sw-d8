<font face="Times New Roman, Times, serif" size="5"><b>Stifling dissent around the Bush family estate</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 30, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">RECENTLY, VOTERS in Kennebunkport, Maine, approved a new Mass Gathering Ordinance which would make obtaining a permit for protests more difficult and more costly--but according to the Maine Civil Liberties Union, it is an unconstitutional infringement of civil liberties.<p>
Kennebunkport was the "Mecca" for dissent this past summer. President Bush spent more time than usual at his family's summer home. He entertained heads of state Vladimir Putin of Russia and Nickolas Sarkozy of France, as well as vacationing with his war-profiteer cronies who "summer" there.<p>
President Bush's visits attracted massive antiwar and impeachment demonstrations that brought the national and international media. Town officials received complaints from the business community, some wealthy retirees "who did not retire to the town to have to see protests in the streets" and, perhaps, even the Bush family.<p>
Whatever the reason, the town wanted to put a lid on the protests, which are growing in size--the August protest was the largest in the history of Maine.<p>
The Kennebunkport town officials created some new rules which would affect some events expected to draw more than 500 people. They also changed the application process, making the permits more difficult to get.<p>
Formerly, permits were granted by the chief of police, Joe Bruni, who I had an excellent working relationship with. He had confidence that my organization would fulfill our end of the agreement. We always "left the grounds and streets cleaner than we found them," according to Bruni. In addition, my organization always took out expensive event insurance to protect the town.<p>
The town officials usurped Chief Bruni's authority to grant permits and gave it to a more politicized board of selectmen, who have broad discretion to exempt weddings, funerals, family gatherings and any event they feel like. The selectmen are now requesting expensive surety bonds for events.<p>
The town told the media the change was prompted by a traveling circus which visited Kennebunkport and was held on private property. However, this reasoning does not hold water--the performance company, Cirkus Smirkus, has a huge liability policy that would protect the town in the event of a claim.<p>
The editorial board of the conservative newspaper, the <i>Portland Press Herald,</i> came out in support of my claim that the new ordinance seems to be directed at protesters. Their editorial said, "Town officials say they don't intend to limit political speech. If that is the case, this should be easy to fix. A new ordinance should specifically say that all demonstrations are welcome. As well as create a hardship exemption for those who cannot afford the bond. That way, future officials will understand that this ordinance was not meant to stop people from exercising their most basic rights."<p>
The Maine Civil Liberties Union and I let the town know that if this law was implemented, we would sue. In less than 48 hours, before the ink was even dry on the new ordinance, it was tabled pending the legal outcome of a similar case in Augusta, Maine, in which the right of free speech won out in the Maine Supreme Court; that decision is being appealed in the First District Court in Boston.<p>
U.S. District Court Judge John Woodcock wrote in his 51-page opinion in the Augusta case: "To march is to speak...a march can be a powerful and effective community expression of ethos: to celebrate our heroes--as on Veteran's Day: to applaud or community-held values--as on July 4th; or, consistent with this country's longest-held traditions, to protest our policies and attempt to effect change--as in Selma or Washington, D.C."<p>
It turns out that the officials failed to get the town's attorney to review the new ordinance before it went on the ballot. Not surprisingly, when Town's Attorney Amy Tchao did review the law, she said, "I don't think the intent was to open up what the board of selectmen, at their whim, could decide to exempt based on content of speech, but that is what it did."<p>
The Kennebunkport town officials are rightly embarrassed by their pathetic and thinly veiled attack on our freedom to assemble, which has been exposed for what it is and will now fail.<p>
We are facing an onslaught of these types of attacks all across America, which are eroding our civil rights. It is imperative that we have engaged citizenry, remaining vigilant to protect our right to dissent.<br>
<b>Jamilla El-Shafei</b>, Kennebunk Peace Department, Kennebunkport, Maine<p>

