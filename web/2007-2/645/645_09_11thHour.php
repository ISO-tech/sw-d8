<font face="Times New Roman, Times, serif" size="5"><b><i>11th Hour </i>asks if time is running out?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Lance Newman</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 21, 2007 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i><a href="http://wip.warnerbros.com/11thhour/" target="_blank">The 11th Hour</a>, </b></i>written and directed by Nadia Conners and Leila Conners Petersen, produced and narrated by Leonardo diCaprio.</font><p>

<font face="Times New Roman, Times, serif" size="3"><i>THE 11TH Hour,</i> the new documentary by first-time directors Nadia Conners and Leila Conners Petersen, powerfully shows the intensity and scale of the environmental crisis.<p>
Produced and narrated by Leonardo diCaprio, this film bombards viewers with shots of devastated industrial landscapes, pollution-fouled rivers, raging forest fires, collapsing ice fields, flooded slums, and the diseased bodies of animals and humans.<p>
These images are interwoven with interviews of many of today's most respected environmental thinkers, including physicist Stephen Hawking, Lester Brown of the Worldwatch Institute and Kenny Ausubel of Bioneers.<p>
The result is a forceful and moving call to action. Unfortunately, the film falls apart when it comes time to say exactly what is to be done.<p>
<i>The 11th Hour </i>makes the important point that what is at risk isn't just pretty landscapes but the survival of human society. It backs up this argument by surveying the biggest threats to the environment: global warming, deforestation, desertification, soil erosion and toxic pollution of air and water.<p>
All of the planet's natural systems are in decline, threatening the most basic physical conditions on which modern society depends, like reliable water cycles, stable weather patterns, and productive farmland, fisheries and forests.<p>
The destruction of society's physical foundation disproportionately affects the poor, especially poor people of color, who face epidemics of environmental diseases like asthma and cancer. The impending "convergence of crises" threatens to destabilize the planet so profoundly that we may face a catastrophic societal collapse. In short, we have reached the moment at which we must take action. We have reached the 11th hour.<p>
The film identifies the right culprits. In one of its most telling sequences, images of post-Katrina New Orleans alternate with footage of President Bush and other prominent politicians describing global warming as an uncertainty and even a hoax.<p>
It goes on to describe how powerful multinational corporations, especially energy companies like Exxon Mobil, have systematically used their spectacular profits to torpedo progressive environmental legislation.<p>
Our society, one interviewee argues, is based on a system of organized greed. "The madness of conventional economics" allows a tiny minority of corporate bosses to steal natural resources that should be managed for the benefit of all people.<p>
Unfortunately, when it tries to explain why the environmental crisis is happening and what can be done to stop it, <i>11th Hour </i>clearly reflects the political weakness of the environmental movement.<p>
The problem, it argues, is one of ideas. The crisis is fundamentally "a thinking disorder." As a society, we have forgotten the "ancient wisdoms," especially the idea that we are part of nature.<p>
Our "minds are out of balance" and we think of ourselves as superior to nature. The solution, therefore, is a "cultural shift" across the whole of society, from the grassroots to the boardrooms.<p>
The rest of the film is dedicated to a mish-mash of prescriptions. Individuals should get involved in environmental organizations. Green designers should re-engineer all of the technologies that support our lives, from power plants to cars. Governments should replace income taxes with carbon taxes and limit corporate political power.<p>
<i>The 11th Hour </i>takes an inspiringly can-do attitude, urging us to see that it's time for "all hands on deck." But the truth it fails to confront is that capitalism is much more than a bad idea. It is a deeply entrenched economic system whose rulers will do anything to defend their right to ravage our lives and our planet for profit.<p>
If we want a healthy society that lives in balance with nature, we will need to overturn the most basic structures of power in our society. We will need to take our common home back from the bosses.<p>

