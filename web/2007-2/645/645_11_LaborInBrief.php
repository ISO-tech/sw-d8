<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">September 21, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>New York City teachers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By John Yanno</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Hundreds of teachers started the new school year without a teaching assignment after the latest round of "reorganization" by the New York City Department of Education.<p>
The teachers, many with years of experience and advanced degrees, worked in District 79, the city's special district for at-risk students. They worked with pregnant students, those preparing to take the GED exam and students with behavioral problems.<p>
Although the Department of Education promised the District 79 teachers would be interviewed for new jobs, 280 teachers were without a position as the school year began. Instead, the city hired lower-paid new teachers, forcing District 79 teachers to accept positions as full-time subs, known as ATRs (absent teacher reserves).<p>
"It's an affront to teachers who served these students, who are very bright and very needy," said one of the excessed teachers, Carolyn Mollica, an Outstanding Teacher of the Year winner.<p>
Mollica and others believe the Department of Education is targeting veteran teachers and trying to force them to retire. "They can get two for the price of one," the 63-year-old Mollica said. "But I'm not ready to retire. If I'm enthusiastic and I love my job, why should I leave?"<p>
A recent change to how the city funds schools makes it a liability for administrators to hire or retain higher-paid veteran teachers. The United Federation of Teachers (UFT), the union representing New York City's 100,000 teachers, failed to launch a fight against this attack on veteran teachers, suggesting instead that they launch individual appeals if they feel they were discriminated against.<p>
This latest attack comes on the heels of the 2005 contract in which the UFT relinquished important seniority rights for teachers who were excessed from their schools, giving principals full control over hiring. Also in the last contract, the UFT agreed to a "voluntary buyout" provision for ATRs who are unable to find jobs, paving the way for teachers to be forced out of the school system altogether.<p>
This latest attack on District 79 teachers is further proof of the need to organize a rank-and-file alternative within our union that can fight to protect the rights of all teachers.<p>

