<font face="Times New Roman, Times, serif" size="5"><b>Conviction thrown out for one of the Jena 6</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 21, 2007 | Pages 1 and 4</font><p>

<font face="Times New Roman, Times, serif" size="3">A LOUISIANA appeals court threw out the only remaining conviction against Mychal Bell, the first of the Jena 6 to be put on trial in an example of modern-day Jim Crow injustice that has stunned people around the globe.<p>
Bell was convicted by an all-white jury of aggravated battery and conspiracy for supposedly taking part in the beating of a white high school student that followed a series of racist incidents against African Americans.<p>
Bell, who was 16 at the time of his arrest, could have been sentenced to 22 years in prison at his September 20 hearing. But the trial judge first threw out the conspiracy charge, and late last week, Bell's battery conviction was overturned as well when an appeals court judge ruled that Bell should have been tried as a juvenile, not an adult.<p>
Civil rights supporters are still planning to descend on Jena by the thousands on September 20 to demand that Bell--who remains behind bars while LaSalle Parish District Attorney Reed Walters appeals the higher court's decision--go free, and that all charges be dropped against the rest of the five.<p>

