<font face="Times New Roman, Times, serif" size="3">The appeals court reversal of Bell's conviction won't affect the other four Black students charged as adults--because they were 17 at the time of the alleged crime and no longer considered juveniles in Louisiana.<p>
Still, the reversal of Bell's conviction is the latest step forward as the case of the Jena 6 gains international attention as a symbol of racist injustice living on in 21st century America.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">THE ROOTS of the case lie in an act of defiance against racism one year ago--when several Black students, after first asking permission from the principal, tried to sit under a tree in the courtyard of the high school, where traditionally only whites gathered at lunch. The next day, white students hung three nooses from the tree.<p>
School officials decided that the nooses were a "prank," and the white students were given brief, in-school suspensions.<p>
Black students received entirely different treatment. For one, Reed Walters was called in to address a school assembly--and the African American students say he was looking at them when he announced, "See this pen? I can end your lives with the stroke of a pen."<p>
In November, three Black high school students were threatened by a white youth waving a shotgun at them in the parking lot of a convenience store. The Black students wrestled the gun away. For this, they were charged with theft of a firearm, second-degree robbery and disturbing the peace. The white youth wasn't charged with a single crime.<p>
At school the following week, a white student allegedly taunted a Black student who had been beaten up at an off-campus party and called several others "nigger." After lunch, he was knocked down, punched and kicked by a group of Black students. The beating victim was taken to the hospital, but was well enough to attend a party that night.<p>
But there was instant retaliation for the Black students. Walters filed charges of attempted second-degree murder against Robert Bailey, Theo Shaw, Carwin Jones, Bryant Purvis, Mychal Bell and another unidentified minor. Walters claimed their "deadly weapon" was a shoe.<p>
After the charges were reduced slightly, Bell went on trial in the first trial of the Jena 6--and was convicted by an all-white jury. Fortunately, however, people like Alan Bean, executive of the Texas-based Friends of Justice group, came to the defense of the youths, building a campaign that brought media attention to the case.<p>
"If the media wasn't watching what was going on, then every last one of those kids would be in jail right now," the mother of Bryant Purvis told <i>Guardian </i>columnist Gary Younge.<p>
Because of the efforts of anti-racists to expose what took place in Jena, Mychal Bell is no longer in danger of spending decades of his young life in prison. But the Jena 6 are not yet free of Jim Crow injustice.<p>
And what Jena represents about racism in the U.S. is still reverberating around the country. The case is an example, wrote Younge "that legal barriers to integration may have been removed--itself no mean feat--but the ultimate goal of equality remains elusive. And it shows that just because you are allowed to do something--even something as basic as sitting under a tree--it doesn't mean that you are able to."<p>

