&amp; reflect on the 10 years<br>
when i was too stubborn to bow to stigmas<br>
i wouldn't listen to the system<br>
i survived on Nydesha's kisses<br>
when oppression was served on the tray<br>
it's a new day<br>
"No Struggle, No Progress"<br>
the vanguard way<br>
from Austin to Ithaca<br>
they heard what we had to say<br>
Rome to Venezuela<br>
i'm not home 'til we persuade 'em<br>
w/ the Power of the People<br>
that I must be free<br>
as long as I have breath in my lungs<br>
freedom is within my grasp<br>
until then I will be<br>
on a Move &amp;<br>
on a DRIVE<br>
alive, kicking &amp; mobilizing<br>
we won't stop 'til we change the tide<br>
one for our love that just would not subside<br>
I'm the phoenix that had to rise<br>
resurrected from the ashes of death row<br>
as I declared in "the prophecy"<br>
victory is mine<br>
victory<br>
is<br>
mine<br>
!.<p>

