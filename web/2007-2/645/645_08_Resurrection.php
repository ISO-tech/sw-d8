<font face="Times New Roman, Times, serif" size="5"><b>Kenneth Foster's "resurrection"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 21, 2007 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">KENNETH FOSTER was, until recently, an inmate on Texas' death row. In an inspiring victory for the anti-death penalty struggle, he won clemency on August 30, just hours before he was scheduled to be executed. The following is a spoken-word piece Kenneth wrote after his sentence was commuted.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="4"><b>resurrection</b></font><p>

<font face="Times New Roman, Times, serif" size="3">i'm the phoenix rising<br>
from the ashes of death row<br>
i stand as the<br>
love that just would not fold<br>
we grew grassroots<br>
under the steel machine<br>
their hate couldn't stop our growth<br>
i'm the hope<br>
reinstilled inside the hopeless capsules<br>
formerly known as men<br>
at the end<br>
i found a way to begin<br>
the catacomb couldn't hold me<br>
i came out more holy<br>
death kissed my lips and said--<br>
"rise up hungry!"<br>
now my appetite is ferocious<br>
for life<br>
not a crumb shall be wasted<br>
as I serve this 3 course meal of<br>
struggle, spiritualism and liberation<br>
and I won't stop<br>
until our nation<br>
is full<br>
i'm lost in the tears<br>
of those that got pulled<br>
into this killer whale<br>
can you feel my smiles across the miles as my lips hold up the pain trickling down my face my brother said--"forgive them for they know not what they do."<br>
i left at 402<br>
but not before I stood<br>
eye to eye w/ the beast<br>
a one way exit<br>
under the release of<br>
governmental poison<br>
but the voices of the people<br>
would not cease<br>
i was at peace<br>
but at war<br>
i'm haunted by the thought<br>
that there will be so many more<br>
i implore the Ancestors as guides<br>
i'm tore by the facts of<br>
genocide on our backs<br>
somebody replay the track to<br>
"walk with me"<br>
the execution chamber was calling me<br>
i responded w/<br>
"HELL NO I WON'T GO!"<br>
they dragged me tho'<br>
the CEDP hit 'em w/ political bombs<br>
to silence their throats<br>
crying for blood<br>
