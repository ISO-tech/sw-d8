<font face="Times New Roman, Times, serif" size="5"><b>A family separated by ICE</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 21, 2007 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">HELLO, MY name is Maria Rodriguez. I have a story to tell. My daughter and I are U.S. citizens, and my husband is a Mexican citizen. Before I got married to my husband, Rafael Espinoza, in the year 2002, Rafael had a visitor visa to cross the border into the U.S.<p>
When he was crossing from the Tijuana border into San Diego, Calif., there was a misunderstanding between the border patrol and Rafael. Rafael, not speaking English too well, was asked if he was a citizen. He misunderstood and thought that the border control man was asking him if he was a visitor. Rafael said yes, and so they took his visa away. Rafael has made some attempts with immigration to fix this problem, but was denied a visitor visa.<p>
Rafael and I were boyfriend and girlfriend for about five years. We finally got married in 2006, thinking that because I am a U.S. citizen, I would be able to fix his residency status, and he would be able to join me in the U.S. In February 2007, I had my first child by Rafael in San Diego. Of course, Rafael was not there because his paperwork was in transit.<p>
In July 2007, Rafael went to Juarez, Mexico, for his appointment to get approved for his U.S. residency. Again, he was denied. Immigration there said that he needed to fix the problem of him saying he was a citizen with U.S. Immigration and Customs Enforcement (ICE) first.<p>
When Rafael returned to Ensenada, Mexico, I contacted my lawyer and mentioned to her what has happening. My lawyer said to me that there is no waiver in order to fix this problem, and that Rafael may never get his visa again, even though my daughter and I are U.S. citizens. I have spoken with several lawyers, and no one can help me.<p>
Border control in Tijuana accused him of something he didn't say. Rafael had all his correct documentation to enter the U.S.--his visitor visa and his passport. He was not trying to commit fraud.<p>
I am now suffering because my husband is living in Ensenada, Mexico, and I am living in San Diego, Calif. I have to drive every weekend to visit my husband with my daughter. I spend about four hours every trip, back and forth, and that's not counting the line at the border.<p>
I do not have my family together because the law has no waiver to fix this problem.<br>
<b>Maria Rodriguez</b>, San Diego<p>

