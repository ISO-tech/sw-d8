<font face="Times New Roman, Times, serif" size="5"><b>The terrible toll of immigration raids</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 31, 2007 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">YOUNG KAYLA wasted no time when offered markers and a poster board after she was informed that we were fresh out of signs. The placard she crafted for the march was more poignant and profound than any of ours. It read: "Make it legal for my Daddy to come back with his family please."<p>

