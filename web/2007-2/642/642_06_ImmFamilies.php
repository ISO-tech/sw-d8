<font face="Times New Roman, Times, serif" size="3">Kayla, like some 1,500 other people, endured 90-degree heat to march for immigrant rights on August 18. Participants walked along Broadway Street to Los Angeles City Hall in a demonstration called by the March 25th Coalition.<p>
The event's demands included an end to raids and immediate amnesty. Many groups participated, including Emergency Response Network members who carried a blue banner inscribed "Alto A Las Redadas" (Stop The Raids). Lively chants accompanied marchers along their route, and speakers addressed the crowd at the end-point rally.<p>
We had an opportunity to interview Kayla's mother, Linda, regarding the story behind her daughter's placard. "My husband was deported five months ago," she said. "He lived here for 36 years, he went to school here. We've been married 28 years, and have six children and seven grandchildren. Now that he's gone, we're struggling. I just had my phone cut off. I can't pay my bills, and one of my daughters is expecting."<p>
Linda's story, like so many others, exposes how current immigration policy and ICE raids break up families. We need to continue protesting and building organizations like the Emergency Response Network to prevent families from being separated.<br>
<b>Robert D. Skeels, </b>Los Angeles<p>

