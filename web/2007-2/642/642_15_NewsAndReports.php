<p><a name="Seattle"></a><font face="Times New Roman, Times, serif" size="4"><b>Stop police brutality in Seattle</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Steve Leigh</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--Fifty protestors rallied here August 26 against rampant police brutality in Seattle.<p>
Sponsored by the Seattle Chapter of the NAACP and other groups, a new Panel on Police Accountability is forming. It will include a group of victims of police brutality in contrast to the city's "Blue Ribbon" commission, which will include politicians and businessmen--none of whom have been victims of police abuse.<p>
Several speakers at the rally highlighted current cases of police brutality in Seattle. On August 18, police attacked an immigrant rights picket with pepper spray, sending three people to the hospital. A janitor working in a downtown building at night was handcuffed and harassed for hours by the police because he looked "suspicious"--in reality, because he was Black.<p>
At the rally, Ophelia Ealy explained the case of her son Michael, who was killed by police eight years ago on his way to a hospital. In addition, the crowd heard how two young Black people were attacked and tasered by police for "looking like gang members," while a young woman was arrested just for objecting to the arrests.<p>
James Bible, President of the Seattle NAACP, called for a renewed campaign to hold police accountable for their brutality and racism, and urged community members to attend court cases and keep the pressure on. Bible also called for the resignation of Police Chief Kerlikowski who has exonerated obviously brutal cops.<p>
Seattle police are out of control, and this rally was a sign that more people are organizing to fight back. It's time to say: "No justice, no peace!"<p>

