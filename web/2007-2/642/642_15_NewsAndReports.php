<p><a name="Veterans"></a><font face="Times New Roman, Times, serif" size="4"><b>Protesting for veterans' health care</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Brian Huseby and Tom Clark</b></font><p>

<font face="Times New Roman, Times, serif" size="3">TACOMA, Wash.--Veterans' health care workers, members of several American Federation of Government employee locals, and their supporters, picketed a field hearing of the U.S. Senate Veterans Affairs Committee here August 17. Other supporters of the picket included members of Jobs With Justice, Military Families Speak Out and the International Socialist Organization.<p>
This year alone, the Bush administration underestimated veterans' health care needs by at least $3 billion--and sent the funds four months late. Now, the administration wants to rob many other social services upon which vets and other workers depend in order to make up the shortfall.<p>
One result of the inadequate funding is that health care workers at VA facilities routinely face understaffing and impossible workloads.<p>
At the hearing, conducted by Sen. Patty Murray (D-Wash.), veterans spoke out about their inadequate care.<p>
Brandon Jones, an Army National Guard soldier deployed to Iraq in 2003 suffers from a sleep disorder as a result of wartime stress. He was offered sleeping pills, but no help with his underlying problem. When the sleeping pills interfered with his military duties, Jones was disciplined.<p>
While at least one vet at the hearing praised Sen. Murray for her support of veterans' health care needs, other attendees pointed out that Murray voted to continue funding the war in Iraq.<p>
