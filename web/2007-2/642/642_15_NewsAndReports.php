<a name="RI"></a><font face="Times New Roman, Times, serif" size="4"><b>R.I. protest against police brutality</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Shaun Joseph</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NORTH PROVIDENCE, R.I.--Approximately 250 people from across the region rallied here August 26 to protest the attack by North Providence police on a labor rights march that resulted in serious injuries to 22-year-old activist Alexandra Svoboda.<p>
The rally was sponsored by the Industrial Workers of the World (IWW) and included members of several area unions and progressive organizations.<p>
Police severely broke Svoboda's leg on August 11 when they moved in to forcibly arrest marchers during an IWW-sponsored picket of Jacky's Galaxy restaurant. Until recently, Jacky's Galaxy was supplied by the New York-based sweatshop HWH/Dragon Land Trading. Eyewitnesses confirm that the marchers were complying with instructions to move off the street when police charged without warning.<p>
Svoboda has since undergone at least four surgeries for the injuries to her leg, and has yet to begin orthopedic reconstruction of her knee. Perversely, she also has been charged with assaulting the officers who broke her leg!<p>
Police brutality and misconduct are a major problem in Rhode Island. Just four days before the attack on Svoboda, a Brazilian immigrant died in the custody of the Woonsocket police when he was denied his anti-seizure medication. The American Civil Liberties Union has exposed systematic racial profiling in several of the state's police departments.<p>
Speakers at the August 26 rally included members of the IWW, its affiliated union Foodstuff Workers 460/640, Students for a Democratic Society, Direct Action for Rights and Equality (DARE), and Rhode Island Jobs with Justice. Demands included an apology from the police, the dropping of all charges and compensation for Svoboda's injuries and lost wages.<p>
As Senia Barragan of the IWW told the crowd, "We must always repeat: An injury to one is an injury to all!"<p>
<i>Messages of support and donations for Svoboda can be sent to: IWW Providence General Membership Branch, P.O. Box 5795, Providence, RI 02903</i>.<p>
