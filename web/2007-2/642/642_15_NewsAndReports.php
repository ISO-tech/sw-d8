<p><a name="Immigrant"></a><font face="Times New Roman, Times, serif" size="4"><b>Immigrant rights in Northern Virginia</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By David Thurston</b></font><p>

<font face="Times New Roman, Times, serif" size="3">MANASSAS, Va.--The Board of Supervisors of Prince William County in northern Virginia passed a vicious anti-immigrant resolution on August 10, which activists are vowing to fight.<p>
The measure denies public services to anyone who cannot produce proof of citizenship, and deputizes local police to work as immigration agents. Within a week, neighboring Loudon County had followed suit, passing a resolution that also denies public services, but stops short of tasking local police with immigration enforcement.<p>
Shocked by the intensity of this assault, immigrant rights activists in the area have convened a series of organizing meetings that have drawn more than 2,000.<p>
"This law is built on hate and racism," said Ricardo Juarez of Mexicanos Sin Fronteras. "Will people be asked for documents in libraries or parks or schools?"<p>
Activists are now planning three steps, including an economic boycott of businesses in Prince William County that are not owned by immigrants from August 27 to September 3. There will also be a mass immigrant rights march on September 2, beginning in Woodbridge. If these steps are unsuccessful, organizers plan to hold a one-day strike of immigrant workers in early October.<p>
About 70 people gathered in the heart of Mount Pleasant, an immigrant enclave in Washington, D.C., on August 18 to hear community members talk about their fears and their determination to organize.<p>
Immigrants in Prince William County will need solidarity from across the region in order to defeat this brutal assault on their basic human rights.<p>
