<font face="Times New Roman, Times, serif" size="5"><b>Protest hits Bush vacation spot</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 31, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>NICK CHIN and HANNAH MORONG report on the antiwar protest of thousands in Kennebunkport.</b><p>

KENNEBUNKPORT, Maine--More than 4,000 people, largely from the Northeast, came to this resort town of the filthy rich to ruin George W. Bush's summer vacation.<p>
The local antiwar group, the Kennebunk Peace Department, organized the march and rally, which featured Cindy Sheehan, war resister Eli Israel, Iraqi solidarity activist Dahlia Wasfi, and Gold Star Families members Carlos and Melida Arredondo, together with representatives of antiwar organizations from across the region.<p>
Following the rally, protesters marched through the town of Kennebunkport past enormous vacation homes and luxury resorts to the Bush family compound.<p>
By far the largest displays of support were reserved for Eli Israel and other members of Iraq Veterans Against the War. Israel, while stationed in Camp Victory in Baghdad two months ago, told his commanding officers that he would refuse to fight.<p>
Israel told the crowd that he had been unable to "reconcile up here [pointing to his head] with what cannot be reconciled in here [pointing to his heart]." He added that as more soldiers come to see the lies behind the war, "the troops will remove themselves from the president's mission."<p>
Though Bush reportedly changed his vacation plans and left town in advance of the protest, the loud, spirited march also passed by the summer mansion of Riley Bechtel, CEO of the Bechtel Corporation--who was met with chants of "Halliburton and Bechtel, take your war and go to hell."<p>
As Jamilla El-Shafei, founder of Kennebunk Peace Department and a leading regional organizer, said, "When people go marching through the enclaves of the rich who have gotten even richer under the Bush administration, they can see that the corporatocracy have these mansions they call their summer houses, where they spend just a few weeks in the summer." She added that it's "an interesting element to take our message right to the people who are profiting from us."<p>
There was a sense of urgency among the protesters about ending the occupation of Iraq. As one member of the crowd expressed the dominant sentiment, "I want to be a part of this. We've just got to get out of Iraq."<p>
There was some confusion, however, about what it would actually take to bring about an end to the war in Iraq. While the crowd cheered speeches by Democrats Cynthia McKinney and Dennis Kucinich--whose slogan is "strength through peace"--they also cheered speakers who were critical of the Democrats' failure to cut funding for the war after their election victories in 2006.<p>
For many speakers, the dominant theme of the protest was putting pressure on Congress. At the same time, by prominently featuring groups like the IVAW, Veterans for Peace and Gold Star Families for Peace, the protest was a significant step forward in furthering the ties between the antiwar movement, and those who have the most power in stopping this war: active duty soldiers.<p>
As Eli Israel said, "What's going to stop [the war]? It has to stop from the inside."<p>

