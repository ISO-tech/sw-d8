<font face="Times New Roman, Times, serif" size="5"><b>Anti-abortionists' new abuse of Kansas doctor</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 31, 2007 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">ONE OF the handful of doctors nationwide who provides later-term abortions is facing legal harassment at the hands of the Kansas court system and anti-choice fanatics.<p>
Dr. George Tiller is facing 19 misdemeanor charges for allegedly seeking second opinions on later-term abortions from a doctor he has financial and legal ties to. Under a Kansas anti-choice law, abortion providers are forced to get second opinions from independent doctors to perform any abortions after the first trimester.<p>
Tiller says he will challenge the constitutionality of the anti-choice law, arguing that is vaguely written and that similar restrictions enacted in other states have been struck down.<p>
Tiller is hated by the anti-abortionists nationally for defying their crusade. His Wichita, Kan., clinic was the chief focus of the notorious 1991 blockade by the anti-choice group Operation Rescue.<p>
This August, Tiller's clinic was closed for three weeks as a result of anti-choice vandalism. On July 4, two masked intruders drilled a hole in the roof of the clinic and flooded a room with a water hose. Tiller was eventually forced to close to repair the damage--and because of increased right-wing protests, he said in a statement.<p>
This month, the judge who was presiding over the case against Tiller recused himself after his record as an anti-abortion politician gained press attention.<p>
Before becoming a judge, Anthony Powell made a name for himself as a leading voice for anti-abortionists in the state legislature. In the 1990s, he accused Tiller of "defying legal and moral authority." As he stepped down from the case, Powell claimed he couldn't remember making such statements.<p>
Kansas isn't the only place that the anti-abortionists have been active this summer, in the wake of their victory earlier this year when the U.S. Supreme Court upheld a federal ban on a late-term abortion procedure wrongly called "partial-birth" abortion by the right wing.<p>
In Aurora, Ill., a city on the western outskirts of Chicago, preparations by Planned Parenthood to open a clinic in September are being disrupted by protests organized by the Pro-Life Action League (PLAL), a group with a record of harassment and abuse at Chicago-area clinics.<p>
At the end of August, more than 1,000 anti-abortionists picketed the site of the new clinic. Anti-choice groups are also trying to pressure the Aurora City Council to prevent the clinic from opening.<p>
"Planned Parenthood should be terrified," PLAL Communications Director Eric Scheidler told a reporter. "We're going to destroy their building, peacefully but aggressively."<p>
Given his organization's history, this statement should be viewed as an active threat.<p>

