<font face="Times New Roman, Times, serif" size="5"><b>What's in the Chicago teachers contract?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jesse Sharkey, CTU delegate, Senn High School</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 31, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--After weeks of tough talk about school officials' mismanagement and intransigence, Chicago Teachers Union (CTU) President Marilyn Stewart reversed field and announced to the House of Delegates August 22 that the union had made a breakthrough in negotiations, reaching a "framework" for an agreement.<p>
Both sides said details were still to be worked out, but the union was expecting a tentative deal as <i>Socialist Worker </i>went to press.<p>
The previous contract between the Chicago Board of Education and the CTU expired at the end of June. The CTU, which represents 32,000 teachers and support staff, stands alone in negotiations after six other unions with 8,000 employees in the Chicago Public Schools reached a tentative agreement with the city in mid-August--a five-year deal with a reported 15.75 percent wage increase.<p>
The 8,000 workers include custodians, security guards, lunchroom workers, special education classroom assistants and engineers, represented by SEIU, UNITE HERE, the Teamsters, the Firemen and Oilers, and the International Union of Operating Engineers.<p>
As for the teachers, Stewart did not give any indication about the basis of the "framework" at the August 22 meeting, but she did tell delegates "to bring their reading glasses" and prepare for a long meeting on August 31 to read and discuss contract language in the tentative agreement.<p>
Delegates could either recommend the tentative agreement for a membership referendum--a system-wide vote conducted at schools, likely within the first week or so of classes--or reject the deal and send the leadership back to the bargaining table. Delegates have almost always approved such agreements--only one tentative agreement has ever been rejected in the history of the union.<p>
The August 22 meeting was sharply different in tone from the August 8 delegates' meeting.<p>
At the earlier meeting, Stewart spent more than an hour denouncing board officials for stonewalling negotiations. She spoke about the union facing three options--"option two" being a strike vote. Delegates continuously interrupted her combative speech with applause.<p>
But at the August 22 meeting, she rarely raised her voice. Even when she offered her opinion that "the talk of option two made them cave," delegates reacted by listening, instead of cheering.<p>
Several delegates came to the floor microphones with advice--"don't flinch," "bring us back a good contract," "don't count an increase in the length of our work day as a raise." But overall, the mood was more like sitting in a waiting room, waiting for word from a doctor.<p>
One thing was clear: The leadership isn't trying to mobilize the union; they've already settled.<p>
Now, teachers will have to wait to see the terms of the settlement. The CTU is in a good position to make real gains for teachers and schools--the budget is growing substantially, and city officials are focused on improving education.<p>
The board may try to give teachers a variation on the deal that other unions got--a 3 percent annual raise, plus pay for extra time added to the school day; some health insurance fixes, including a wellness program; and, finally, some job security protections.<p>
But will the agreement address the growing nonunion charter schools? Will it provide improvements on class size and working conditions? Will elementary teachers finally be guaranteed a prep period?<p>
In the end, how much teachers get will likely depend more on the 32,000 members than the toughness and charisma of our leader.<p>

