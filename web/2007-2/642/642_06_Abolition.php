<font face="Times New Roman, Times, serif" size="5"><b>A prime example of the need for abolition</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 31, 2007 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">MARK CLEMENTS is a victim of torture at the hands of Chicago police and has been incarcerated since age 16--serving four life sentences, plus 30 years--for a crime he did not commit. He wrote the following letter about the case of Texas death row inmate Kenneth Foster.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">BESIDES THE state of Illinois, the state of Texas is a prime example of why the death penalty in this country needs to be abolished now. Texas has executed the mentally ill, and innocent people such as Cameron Todd Willingham, Ruben Cantu, Carlos de Luna and Frances Newton.<p>
Now, if Texas prosecutors have their way, Mr. Kenneth Foster Jr. will be put to death by lethal injection on August 30 for driving a car. He drove a friend around. He parked the car with the windows rolled up, and listened to loud music. Mr. Foster's friend left the car, and shot and killed a man 80 feet away.<p>
Mr. Foster did not give his friend the gun to carry out this shooting. He did not tell or order his friend to do this shooting. He did not drive his friend to this location with the purpose to commit a shooting. And he did not know what his friend did.<p>
His friend has since been executed for this crime, and said that Mr. Foster was innocent. This should have put an end to this case.<p>
Over the past four decades, no other race has suffered more from the humiliation of injustice at the hands of the police and criminal injustice system in this country than African Americans. Whether it's Chicago police torturers, who still have most of the men they tortured locked in state prisons for crimes they did not commit, or suspicious beating deaths by prison guards upon African Americans, this race has suffered at the hands of those who are elected, appointed or hired to "protect" them.<p>
What did the state of California gain by killing Stan Tookie Williams? What will Texas gain by killing poor Kenneth Foster? Nothing.<p>
Mr. Foster would not be on death row for this offense if he was rich. The state of Texas would not have charged this man with this crime. For the state of Texas to have this man on death row is an insult. It's ridiculous, and should outrage elected politicians the same way it does the many supporters of Mr. Foster.<p>
I respectfully request and pray in this case that the honorable Gov. Rick Perry grant Mr. Foster a pardon, effective immediately.<br>
<b>Mark Clements, </b>Free the 26 Movement, Pontiac, Ill.<p>

