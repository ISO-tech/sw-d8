<font face="Times New Roman, Times, serif" size="5"><b>Making a change, starting tonight</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 31, 2007 | Page 11</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Mavis Staples, <b><i><a href="http://www.amazon.com/gp/redirect.html?ie=UTF8&location=http%3A%2F%2Fwww.amazon.com%2FWell-Never-Turn-Mavis-Staples%2Fdp%2FB000MR8SZU%3Fie%3DUTF8%26amp%3Bs%3Dmusic%26amp%3Bqid%3D1188326559%26amp%3Bsr%3D1-1&tag=socialistwork-20&linkCode=ur2&camp=1789&creative=9325" target="_blank">We'll Never Turn Back</a></i>, </b>Anti-Records, 2007.</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>NICOLE COLSON reviews a new album by gospel great Mavis Staples.</b><p>

IF YOU thought hip-hop artist Kanye West's take on Hurricane Katrina ("America is set up to help, the poor, the Black people, the less well-off, as slow as possible...George Bush doesn't care about Black people") was right-on, you'll want to hear what Mavis Staples has to say.<p>
Staples, perhaps best known to younger audiences for classic soul hits like "I'll Take You There" and "Respect Yourself," returns to her gospel and protest music roots on <i>We'll Never Turn Back</i>--an album of freedom songs designed to inspire a new generation in struggle. After a listen, it should become apparent why <i>Rolling Stone</i> once declared Staples "the most underrated diva of the century."<p>
<i>We'll Never Turn Back </i>is planted firmly in the tradition of Mavis' family group, the great Staple Singers, which became a voice of the civil rights movement in the 1960s with recordings of traditional gospel spirituals as well as more explicit songs of social protest.<p>
The 67-year-old Staples is in tremendous voice, here produced and backed on guitar by the always-interesting Ry Cooder, with additional vocals from Ladysmith Black Mambazo and members of the Student Nonviolent Coordinating Committee (SNCC) Freedom Singers. Cooder's guitar work complements Staple's voice particularly well, showcasing and lifting her smoky alto, without ever overshadowing it.<p>
Traditional songs of the civil rights movement like "This Little Light," "Eyes on the Prize" and "We Shall Not Be Moved" are re-imagined with an emphasis on blues, soul and funk. "Eyes on the Prize," for example, is shaped by Cooder's funk-oriented slide guitar, which plays off Staples' voice to great effect. The song is made immediate, relevant and ominous--a warning about the consequences of the failure to fight against injustice.<p>
"This Little Light," meanwhile, is turned into a saxophone-infused, swamp-rock stand against the war. "On the battlefield, I'm going to let it shine," Staples sings, "See now, I ain't going to fight in no rich man's war/That ain't what God wants to use me for." If my Sunday school teacher had sung "This Little Light" with one-tenth Staples' passion, I might have actually paid more attention in church.<p>
The album's most chilling song is "In the Mississippi River," a duet with Charles Neblett that tells the story of the dredging of the Mississippi River in 1964 to look for the bodies of slain civil rights workers James Chaney, Andrew Goodman and Michael Schwerner. In the search for their bodies, one after another after another of the bodies of Blacks who had been lynched were pulled from the river.<p>
"You can count them one by one/It could be your son/You can count them two by two/It could be me or you," Neblett and Staples alternate, as the song's winding bass line echoes the current of the river itself. "We've got to stop them from going in the river."<p>
Sadly, one song that didn't make it onto the album was an offering from Public Enemy's Chuck D titled "Freedom's Got a Shotgun," which Staples apparently felt was too hot to record. But that shouldn't suggest that the remaining tracks are tame in any way.<p>
"My Own Eyes," the longest track on the album at more than seven minutes, is Staples' own remarkable story--beginning with a night 40 years ago that the Staples Singers spent in an Arkansas jail because of racist cops, the group's involvement in the civil rights movement, and linking those experiences to the abandonment of Blacks in New Orleans during Hurricane Katrina.<p>
"I saw New Orleans, saw the people left for dead/I heard every bald-face lie you politicians said," Staples sings with righteous anger, as Cooder's guitar work builds from a sweetly tentative to an insistent melody that drives the song forward. "I've seen it for myself, and you can't fool your sight/Well we'd better make a change and we'd better start tonight," Staples sings.<p>
As Staples told the <i>Chicago Sun-Times'</i> Dave Hoekstra in April, "When Katrina happened, I had flashbacks. I thought about Bloody Sunday [the 1965 Selma-to-Montgomery March where state and local lawmen attacked voting rights marchers with billy clubs, hoses and tear gas].<p>
"People are still looking at Black and white. Now is the time for civil rights songs again."<p>

