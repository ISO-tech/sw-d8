<font face="Times New Roman, Times, serif" size="3">She went on to say that the shirts provided an "opportunity for girls to express that they are part of New York City society...and shaking off oppression."<p>
The following day, the right-wing <i>New York Post </i>published the headline "Intifada Principal" for a story claiming that the shirts were "apparently a call for a Gaza-style uprising in the Big Apple."<p>
There are no ties between the planned school and the AWAAM, but the nonprofit group shares office space with Saba: The Association of Yemeni Americans, which counts Almontaser as one of its board members.<p>
Almontaser later apologized for not addressing the historical connotations of the word Intifada, and then resigned as principal, out of fear that continued media attacks would could stop the school from opening. Ralliers on August 20 called for her to be reinstated.<p>
The Khalil Gibran Academy, named after a famed Lebanese-American Christian poet who promoted peace, would be one of a few nationwide that incorporate the Arabic language and culture. Though the school is still scheduled to open, demonstrators fear that the attacks will continue. "They want to increase racism and destroy anything associated with being Arab or Muslim," Ibrahim Qatabi said at the rally.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">THE RACIST campaign against the school has been led by right-wing organizations calling themselves the Stop the Madrassa Coalition. On its advisory board are Frank Gaffney, founder and president of the Center for Security Policy, a neoconservative think tank, and right-wing historian Daniel Pipes, director of the McCarthyite Web site Campus Watch, which claims to "monitor Middle East Studies on campus."<p>
The campaign found sympathy among the right-wing media. The <i>New York Sun </i>reported that the school could "groom future radicals."<p>
As Debbie Howard, a design team member for the new academy, commented, "That the Department of Education would let a tabloid newspaper influence policy is unacceptable." Howard went on to question the lack of support from Mayor Michael Bloomberg and Schools Chancellor Joel Klein for "a fabulous educator."<p>
In a move that outraged many teachers in New York City, United Federation of Teachers President Randi Weingarten jumped on the right-wing bandwagon in attacking Almontaser in a letter to the editor of the <i>New York Post</i>.<p>
"It is very disturbing to read about Almontaser defending the use of the term 'Intifada NYC,' and I agree wholeheartedly your editorial denouncing the practice," Weingarten wrote. "While the city teacher's union initially took an open-minded approach to this school, both parents and teachers have every right to be concerned about children attending a school run by someone who doesn't instinctively denounce campaigns or ideas tied to violence."<p>
Many teachers and community groups said that Weingarten's comments legitimized the racist diatribe in the right-wing media and helped to force Almontaser to resign from her position. "We believe your statements have played a role in furthering the anti-Arab and anti-Muslim racism that pervades and infects our city," the Center for Immigrant Families wrote in a letter to Weingarten.<p>
Teachers at the press conference said that Weingarten's views did not represent those of many rank-and-file members of the union, and joined the call for Almontaser to be reinstated.<p>
Attacks on the Arabic and Muslim community have become commonplace in the media and among elected officials.<p>
"We look forward to a day when our officials condemn the racism that created this situation rather than affirm it...and when our popular press outlets report news rather than instigate it," Mona Eldahry said at the demonstration. "I stand here today with the people of all races and backgrounds to say: we will not be silent."<p>
As Rev. Clinton Miller of the Brown Memorial Baptist Church in Brooklyn put it, "We already know what happens when religious fanatics use hysteria to rally up the citizenry. That's what they did to unjustly finance the immoral war in Iraq, and if we're not careful, they'll do it again in our school system."<p>

