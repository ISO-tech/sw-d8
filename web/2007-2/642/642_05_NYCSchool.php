<font face="Times New Roman, Times, serif" size="5"><b>NYC media's racist crusade against Arab principal</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 31, 2007 | Page 5</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>DARREN VERACRUZ and MEGAN BEHRENT report on the attack on a New York City school.</b><p>

SOME 300 people rallied in New York City in support of the Khalil Gibran International Academy and its former principal Debbie Almontaser at an August 20 demonstration in front of the city's Department of Education headquarters.<p>
The school, which is scheduled to open this fall, has been subjected to sustained attacks by right-wing media outlets and racist organizations ever since plans for its creation were announced earlier this year.<p>
Those attacks reached a high point this summer when Almontaser was pressured to resign after media outlets lambasted her statements regarding a T-shirt, produced by Arab Woman Active in the Arts and Media (AWAAM), bearing the slogan "Intifada NYC."<p>
Almontaser said the word Intifada "basically means 'shaking off.' That is the root word if you look it up in Arabic. I understand it is developing a negative connotation due to the uprising in the Palestinian-Israeli areas. I don't believe the intention is to have any of that kind of violence in New York City."<p>

