<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">August 31, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Seattle security guards</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Daniel Troccoli</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--Some 60 people marched and rallied August 24 in support of union recognition for security guards at five companies in the area that employ over 700 unarmed guards, including nationwide corporations Securitas and Allied Barton.<p>
The workers have been trying to organize local security guards into Service Employees International Union (SEIU) Local 6. Many security guards in the area, who receive an average wage of $11.50 an hour, are forced to pay for their health care benefits.<p>
Jack Owens, a 20-year security guard, is one such worker. An employee of Securitas, his health care benefits were cut off when he was recently transferred to a new client. He was offered continued coverage only if he paid the monthly COBRA fee of $350 on his $11 an hour wage. "We need better health care for all," he said. "They can't be selective of who they cover."<p>
Companies like Securitas claim they need to allow clients the choice of whether to pay for their employee's health care in order to remain competitive. However, Securitas can hardly cry poverty with revenues topping $3 billion last year. Also, security guards get no paid breaks or paid sick or holiday leave.<p>
As of last week, three of the five companies had agreed to begin negotiations after Local 6 proved that 75 percent of the workers wanted the union.<p>
However, Northwest Security and Star Protection Agency stubbornly refuse to sit down at the table. During the protest march, organizers stopped at particular buildings to spotlight Star Protection, chanting, "We clean your office, we guard your halls, we demand justice and health care for all!" and "24 hours, 7 days a week, security officers turning up the heat!"<p>
Northwest Security held a mandatory meeting where they explained the benefits of working in a non-union workplace telling their workers, "We care about you. We offer you love."<p>
With support from other local unions and Jobs with Justice, SEIU and security guards pledged to keep up the pressure on the companies holding out until security officers are organized across the board.<p>

