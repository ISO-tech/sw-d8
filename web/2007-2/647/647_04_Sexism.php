<font face="Times New Roman, Times, serif" size="5"><b>The wrong way to battle sexism</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 5, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">ON SEPTEMBER 25, Rep. Bobby Rush (D-Ill.) held a congressional hearing on the content of rap lyrics. The hearing was titled "From Imus to Industry: The Business of Stereotypes and Degradation." As the title suggests, the investigation is directly connected to the April 4 remarks of Don Imus.<p>
While laughing with producer Bernard McGuirk on his MSNBC show, Imus called the Rutgers' women's basketball team "nappy-headed hos." Imus' racist and sexist comment created a backlash of anger and protest that got him fired in a matter of days.<p>
Before leaving, however, he tried to rationalize his degrading remarks by blaming rap lyrics. Many have taken the argument behind Imus' childish finger-pointing as a cause to rally around. According this logic, Snoop Dogg is now responsible for Imus' racism; Jay-Z is the cause of his misogyny.<p>
Many in the Black community, as well as the more obviously bigoted Imus supporters, have taken up this call to arms that blames Black artists for racism. The short-lived "Ban 50 Cent" organization is an example, and Rush's congressional hearing is another.<p>
Rush--the man who said Barack Obama's election to the Senate was "divinely ordered"--asked rap artists and music industry executives to appear before the subcommittee and answer questions, for a hearing on "a timely issue and one that won't go away." The issue, however, is quite old, and for those of us who remember, it can be traced to the 1990s rap witch-hunt by politicians like C. DeLores Tucker.<p>
Targeting what the media began to call "Gangsta Rap," Tucker protested at stores, picketed music company offices and created the same CD-stomping atmosphere we saw around the Dixie Chicks. Imus' disgusting comments are now being used to recreate that same climate.<p>
Rush is quote by <i>Variety</i> as saying, "I want to talk to executives at these conglomerates who've never taken a public position on what they produce," Even if this banal statement is what the whole investigation will amount to, it still rests on a backwards reasoning.<p>
For many, the argument is easy to agree with. Looking at the rap songs on the Billboard Top 20, you're confronted with a barrage of violent and misogynistic lyrics.<p>
But there are two things to keep in mind. First, the rap songs on the radio don't reflect the whole art form any more than McDonalds' popularity reflects the culinary height of a Big Mac. Both are cheap and easy to make, so they benefit the profit-driven companies who sell them.<p>
Second, we live in a system that thrives on sexism, homophobia and racism--the roots of which aren't rap lyrics. We have to make the argument political rather than just cultural.<p>
The fact is that many of the right-wingers who point the finger at musicians are the same ones who fight tooth and nail to deny abortion rights to women in their district; who applaud the death penalty as an act of justice; and who become livid at even the idea of gay marriage. The policies of the government help create an atmosphere where sexist rap lyrics are normalized.<p>
In defending his music, Nas slammed Fox News' Bill O'Reilly and said, "Let him ask why I made the songs I made. It didn't come from nowhere. It came from this country. I'm not talking about Russia in my music...I'm not talking about Africa, Switzerland, China. I'm talking about me being American and growing up in a crazy world...reflect[ing] all different sides of life."<p>
There is a real reason to be angry at the sexism expressed in our culture, but looking for it to be ended from on high, based on a moral imperative, is a waste of time. Instead, activists should be spending their time fighting those who create a sexist world, not just individuals who express the symptoms of it.<br>
<b>Alex Clermont, </b>New York City<p>

