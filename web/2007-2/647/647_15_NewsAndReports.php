<a name="Health"></a><font face="Times New Roman, Times, serif" size="4"><b>Vigil for single-payer health care</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Alison McKenna</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--About 75 people participated in a September 28 protest and vigil for single-payer health care at the State of Illinois Building organized by the Chicago Single Payer Action Network (CSPAN). The event was part of a national day of action initiated by American Patients for Universal Health Care (APUHC), a group started by Donna Smith, who appeared in Michael Moore's movie <i>Sicko</i>.<p>
APUHC's vigil in Washington, D.C., focused on Tracy Pierce, who died from kidney cancer at the age of 37, after being denied the care he needed. There were also similar actions in Kansas City, Mo., Oklahoma City, Okla., Denver, and Louisville, Ky.<p>
Stephen Skvara, a retired member of the United Steelworkers union (USW), spoke at the Chicago CSPAN event about how he and his family lost their health care after LTV Steel in Indiana declared bankruptcy. He had worked at LTV for 34 years.<p>
Illinois State Rep. Mary Flowers, the main sponsor of a statewide single-payer bill known as HB 311, took Gov. Rod Blagojevich to task, explaining that his measures for women's health, which include screening and treatment for breast and cervical cancer, don't provide comprehensive care and that health care cannot be provided piecemeal.<p>
Other notable speakers were Dr. Anne Scheetz from Physicians for a National Health Program, Tom Wilson from Access Living, Helen Redmond from CSPAN, and a Trinity Hospital nurse from the National Nurses Organizing Committee. Other groups represented included Health Care for All Illinois, HealthCare Now! the International Socialist Organization, Dennis Kucinich's presidential campaign, Metropolitan Seniors in Action and the Progressive Democrats of America.<p>
Participants chanted, "Health insurance is a lie! They don't care if people die!" and circulated a petition in support of HB 311.<p>
CSPAN's next action is a town hall meeting in early November with Rep. Flowers to continue building support for HB 311.<p>
<i>The next CSPAN meeting is October 10 at 6:30 p.m. at Access Living, 115 W. Chicago Ave. For more information, contact 312-714-5154 or e-mail <a href="mailto:chisinglepayer@gmail.com">chisinglepayer@gmail.com</a></i>.<p>
