<p><a name="Portland"></a><font face="Times New Roman, Times, serif" size="4"><b>Marching against war in Portland</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul Dean</b></font><p>

<font face="Times New Roman, Times, serif" size="3">PORTLAND, Ore.--A spirited march of more than 600 people weaved through downtown on September 29 to demand an end to the wars in Iraq and Afghanistan.<p>
Protesters also marched to bring the troops home immediately, called for an end to Israel's occupation of Palestine and opposed a U.S. attack on Iran. The march was energized by chants such as "Money for jobs and education, not war and occupation!"<p>
Speakers along the route and at the final rally included Ghourdz Eghtedari, an Iranian man who exposed the lies about Iran peddled by Bush and his media hacks. A former Black Panther spoke about Portland resident Lumumba Ford, a Black Muslim who was sent to prison under the USA PATRIOT Act. ISO member Adam Sanchez spoke for PDXpeace.<p>
This year's march was significantly smaller than a march a few years ago that drew more than 20,000. Nevertheless, there are important ongoing antiwar activities in the city, including counter-recruitment activism and making Portland a sanctuary city for military war resisters.<p>

