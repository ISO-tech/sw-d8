<p><a name="SantaCruz"></a><font face="Times New Roman, Times, serif" size="4"><b>Anti-immigrant racism at UC-Santa Cruz</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jessi Shakarian</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SANTA CRUZ, Calif.--Some 50 students at the University of California-Santa Cruz (UCSC) started the school year by protesting an anti-immigrant dodge ball game set up by the College Republicans.<p>
The Republicans sent out an e-mail, proposing a dodge ball game at the annual Office of Physical Education, Recreation and Sports festival, where students gather to join campus organizations. The game would pit "Team Border Patrol" versus "Team Illegal Immigrants," with Team Illegal Immigrants "outnumbering" Team Border Patrol.<p>
In response, an emergency meeting and coalition was formed, and a picket was assembled when the dodge ball game was supposed to happen. The protesters, carrying signs such as "Fighting racism at UCSC," easily outnumbered the four Republicans. And the Republicans, knowing that the game was over, left the field.<p>
The speed with which this coalition was assembled shows that people want to be involved in the struggle to stop racism and that the struggle has the potential to win.<p>
