<font face="Times New Roman, Times, serif" size="3">What took place was a <i>real</i> proletarian movement. It was truly inter-communalist in the way it was carried out: all colors, all beliefs and all genders. There's nothing more exhilarating than this, in my opinion. It shows what can be done when people unite and put minor differences aside.<p>
Since I began my rise to consciousness, I had faith in our beliefs, and I am now a new child, birthed from the loins of these revolutionary actions. And the whole time, Campaign to End the Death Penalty and ISO members were at the forefront.<p>
This should act as a spark in the prairie. There's actually no way to turn back or accept any less at this point. How could we? While it's true that the killing machine continues, it continues with a <i>dent</i> in its armor. We do face a huge beast, and we have to break it bit by bit.<p>
What I would like for all our comrades--and even those just on the sidelines--is for saving my life to be an investment on the future of the movement.<p>
I'm not going to be one of these guys who gets off death row and goes about my business. Death row is my business. It became my <i>personal</i> business when it put its boot on my neck, when it subjected me to what it did. Now, it can never lose my attention. Now it's personal, and it's for life! I'm looking forward to dealing with it.<p>
And now I am in a new environment and mentalities are different, so I have new obstacles to overcome. Like some comrades say. we have to turn these slave camps into revolutionary camps. The soil of revolution is always fertile in conditions of oppression, so I'm like a farmer with much land to till.<p>
This is my calling, and I will step up to the challenge with no fear or hesitation. I plan to be as serious as George Jackson was when he was in Soledad and San Quentin.<p>
There will much more to come. But for now, I only wanted to say that what has been done is historic, and no one can take that away from us. It touched the world. The tide is turning. We must continue to be political tsunamis.<p>
I extend these words with nothing but love and strength in my soul, and I hope that all you comrades will find the time to sacrifice like those down in Austin did. Those people are champions. Together, we can be conquerors of injustices.<p>
Let this victory stand as a flame in your spirit and please...never let it extinguish. A people's army salute to you all.<p>
In solidarity for life!<br>
<b>Haramia KiNassor (Kenneth Foster Jr.), </b>Beeville, Texas<p>

