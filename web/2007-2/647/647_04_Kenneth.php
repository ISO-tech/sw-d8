<font face="Times New Roman, Times, serif" size="4">A letter from Kenneth Foster:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>We touched the world</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 5, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">KENNETH FOSTER was saved from execution by the Texas death machine when Gov. Rick Perry granted clemency--Perry's first such commutation in his years as the Texecutioner--hours before Kenneth was scheduled to be executed on August 30. Kenneth wrote this letter to express his thanks to members of the International Socialist Organization.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">I OPEN this missive with nothing but revolutionary love and deep respect for all you comrades out there on the front lines of our movement.<p>
I felt compelled to send these words of solidarity to my International Socialist Organization (ISO) brothers and sisters, due to the fact that the ISO has shown me so much love throughout my plight, and I wanted you all to know that there is a great gratitude there.<p>
I cannot hold back the truth of the fact that from May 30 to August 30, we witnessed a true people's movement. Many people say "Power to the people," but have never even seen it in action. Well, it was just shown across the world.<p>

