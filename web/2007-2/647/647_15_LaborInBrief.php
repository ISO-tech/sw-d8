<a name="Security"></a><font face="Times New Roman, Times, serif" size="4"><b>Bay Area security officers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jessica Hansen-Weaver</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Members of the Bay Area security officers union, Service Employees International Union (SEIU) Local 24/7, took to the streets for a week of action September 24-28 in a struggle for living wages and health care benefits.<p>
After months of failed negotiations with property owners and security contractors, workers have had enough. On September 15, Local 24/7--which represents nearly 6,000 security officers in San Francisco, Alameda and Contra Costa counties--voted overwhelmingly to authorize an unfair labor practice strike if necessary.<p>
"We are ready to strike," said security officer Marcia Duncan. "If that's what it's gonna take, then we are ready."<p>
On September 24, members marched from a rally in downtown San Francisco through the financial district. Many came out to support the workers, including Supervisor Chris Daly, Amos Brown from the NAACP, the African American Action Network, Teamsters, and a representative from Sen. Carol Migden's office. Some bike messengers and other building visitors are honoring picket lines.<p>
Activists are taking aim at the largest property owners in San Francisco, including Morgan Stanley and Shorenstein and Hines. These firms have recruited replacement workers, who are in place in the struck buildings.<p>
Members of Local 24/7, a predominately African American and immigrant union, are among the lowest-paid in the commercial property services industry. While Local 24/7 members protect multibillion-dollar buildings in Bay Area's financial district, the average income for a security officer in the Bay Area is less than $24,000 a year.<p>
"A lot of officers have to work two or three jobs just to make ends meet," said SEIU member Keith Ward. He said many officers work seven days a week working multiple jobs.<p>
After 25 years as an officer, he still doesn't have health benefits. He uses the extra income he makes from garage sales as an emergency fund.<p>
