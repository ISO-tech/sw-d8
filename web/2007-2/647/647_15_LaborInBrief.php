<p><a name="UC"></a><font face="Times New Roman, Times, serif" size="4"><b>University of California patient care and service workers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Kathryn Lybarger, Bargaining Representative, AFSCME Local 3299, UC Berkeley</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AT RALLIES across California on September 27, more than 1,800 service and patient care workers and supporters turned out at the campuses and medical centers of the University of California (UC) system to demand better pay and protections over health and pension benefits.<p>
The patient care contract expired September 30, and the service contract expires January 30. This kind of turnout at the start of the campaign shows the depth of the anger shared by workers at UC who have fought successfully in the last couple years against pay cuts to fund our pension, and have seen huge increases in our health care costs.<p>
We have seen our wages fall behind the market rate while the office of the outgoing UC president was exposed for its scandals involving unregulated executive compensation.<p>
A host of other issues, like insufficient staffing levels and mandatory overtime, compound UC workers' frustration, and make service worse for students and dangerous for patients.<p>
UC officials know they have a big battle ahead, so they've hired the p.r. firm Hill and Knowlton--which represented Big Tobacco and Enron, and helped promote Bush Sr.'s Gulf War.<p>
Management is right to be nervous. This will be a huge fight for us, but one we can win. We can build on our unity and energy to win a contract that makes UC a university that's for students, patients and workers--not for greedy executives.<p>
