<p><a name="Providence"></a><font face="Times New Roman, Times, serif" size="4"><b>Providence Health and Services</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Kate Johnson</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--Chanting "Fired up, won't take no more!" some 200 health care workers, union organizers and activists from Portland, Ore., and Seattle marched to Providence Health and Services headquarters here on September 24 to protest the company's anti-union policies.<p>
The spirited crowd outside jeered at management as a delegation of Portland Providence workers and union officials with Service Employees International Union (SEIU) Local 49 met with executives to demand fair union elections at the hospital.<p>
Providence employees have been trying to form a union with Local 49 for about two years. Last summer, the Workers Rights Board Fair Election Oversight Commission determined that the company's anti-union tactics made it impossible for hospital workers to hold a fair union election until Providence agreed to fair election ground rules.<p>
Providence has been viciously anti-union since some of its workers first organized in 1985. It hired the notorious union-busting law firm Jackson Lewis, forced Portland employees to attend mandatory anti-union meetings and illegally fired union activists.<p>
Providence is the largest and most profitable "non-profit" hospital chain in the Northwest, a feat it managed by slashing staff, charging exorbitant fees and buying out competitors.<p>
While Providence says it's too poor to hire more staff and pay better wages, CEO Henry Walker retired in 2004 with a golden parachute worth $5.5 million.<p>
Providence workers, who represented a third of the protest, are clearly fed up with their employers' unfair treatment. More rank-and-file involvement and solidarity will be key to the Portland Providence workers' success in winning a union and improving their working conditions.<p>

