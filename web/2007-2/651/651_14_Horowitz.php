<font face="Times New Roman, Times, serif" size="5"><b>David Horowitz's Islamophobia week</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 2, 2007 | Page 14</font><p>

<font face="Times New Roman, Times, serif" size="3">HUNDREDS OF students, faculty and community members across the country turned out to protest "Islamo-Fascism Awareness Week"--a campus tour of intolerance against Muslims and the left spearheaded by right-wing pundit David Horowitz.<p>
Held in conjunction with right-wing student groups, the tour featured prominent right-wingers like Ann Coulter and former Sen. Rick Santorum. But Horowitz and his cronies were met with fierce opposition wherever they showed up.<p>
--In <b>Madison, Wis., </b>more than 100 turned out October 22 to confront Horowitz as he made his appearance at the University of Wisconsin. Members of the Black Student Union, the Multicultural Student Coalition, College Democrats, the Campus Antiwar Network and the International Socialist Organization came out in opposition.<p>
Horowitz derided those who opposed him as supporters of terrorism and suggested that they be treated as war criminals. When the question-and-answer period came, more than 20 activists went to the line with questions for Horowitz--including how he justifies U.S. support of repressive regimes.<p>
In the end, he dodged the questions and instead hurled more tired insults at those who questioned him.<p>
--In <b>Seattle, </b>on the same day, more than 200 picketed right-wing talk radio host Michael Medved at the University of Washington (UW). Medved had been scheduled to speak on the threat of "Islamo-Nazism" and the "special violence problem in Islam."<p>
In advance of the event, a letter of solidarity against "Islamo-Fasicsm Awareness Week" was signed by hundreds of students, faculty and staff and printed in the <i>UW Daily</i>. More than 20 campus groups turned out to protest, with demonstrators joining in with chants of "End Islamophobia!" and "No justice, no peace--end the war in the Middle East!"<p>
--In <b>Los Angeles, </b>75 protesters gathered outside the Anneneberg School of Communications at the University of Southern California (USC) where Ann Coulter spoke October 24.<p>
Students from the USC Antiwar Coalition, the Muslim Student Union, Students for Justice in Palestine, Students of Color in Action and the Women's Creative Collective for Change came together with other students and local activists to organize a picket and speak-out against Coulter's appearance, which was billed as a talk about the "violent oppression of women in Islam."<p>
Shireen, a USC graduate student, said she joined the protest because "we're being demonized...Islamo-Fascism Awareness Week is just about hate."<p>
-- In <b>Atlanta, </b>protesters at Emory University succeeded in driving Horowitz from the stage with chants of "Racist, sexist, antigay--David Horowitz go away!"<p>
<i>Ben Daniels, Elizabeth Fawthrop and Cindy Kaffen contributed to this report</i>.<p>

