<font face="Times New Roman, Times, serif" size="5"><b>Why did the Feds refuse to act on Jena?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 2, 2007 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">ACTIVISTS ARE organizing to get their message to the Louisiana courts: We want justice for the Jena 6!<p>
The Jena 6--six African American high school students charged with assault in the wake of a schoolyard fight prompted after several racist incidents, including nooses hung from a tree in the courtyard of Jena High School--face an important court date in early November.<p>
On November 7, four of the six--Theodore Shaw, Robert Bailey, Bryan Purvis and Mychal Bell--will reportedly be in court for pre-trial hearings.<p>
Bell is currently incarcerated, after his bail was revoked and he was given a sentence of 18 months when a judge recently ruled he violated probation in an earlier juvenile case.<p>
Now, some civil rights activists and talk radio hosts are calling for a November 2 economic "blackout" in order to protest the federal government's handling of the case. Though the impact may not be massive, the point, says radio personality Warren Ballentine, is to make a statement.<p>
And on November 16, Rev. Al Sharpton is planning to lead a march in front of the Department of Justice in Washington, D.C., to protest the federal government's failure to act on hate crimes and racial attacks. "Since the federal government won't come to the people, we're going to bring the people to the federal government," Sharpton said at a recent press conference.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">THE NEED for such action was made abundantly clear at an October 16 congressional hearing that exposed the indifference of the Bush administration's Justice Department to the depths of racism in the U.S.<p>
The House Judiciary Committee hearing included federal officials and civil rights and community activists. Congressional Democrats, including Reps. John Conyers and Sheila Jackson Lee, questioned why the Justice Department failed to respond to the hanging of nooses in Jena--or to intervene when local District Attorney Reed Walters brought overly harsh charges against the six Black teens.<p>
Conyers said Walters had been invited to testify, but he "declined."<p>
Republican lawmakers on the panel, however, displayed near contempt. Texas Rep. Lamar Smith accused the hearing of "stoking racial resentment." And responding to the panel's invitation to civil rights activist Rev. Al Sharpton, Howard Coble of North Carolina told Conyers, "If I were compiling a group of witnesses to encourage the diminishing of racial disharmony, I don't know that Mr. Sharpton would have made my cut."<p>
The biggest fireworks of the day came when Sheila Jackson Lee exploded at Donald Washington, the U.S. attorney for the Western District of Louisiana--and the first African American ever appointed to that position--for his failure to take action.<p>
"Tell me why you did not intervene," Jackson Lee shouted at Washington. "These broken lives could have been prevented if you had taken the symbolic responsibility that you have, being the first African American appointed to the Western District."<p>
Washington admitted that the hanging of nooses constituted a federal hate crime, but claimed that that federal authorities decided not to prosecute because of the ages of the youths involved.<p>
But many say Washington's story has changed in the wake of media attention surrounding the case. At a July Department of Justice education forum in Jena, for example, Washington would not call the noose hanging a hate crime, and he told the crowd that he had looked at pictures of events like football games to gauge the level of racism in the town. "I didn't see from the pictures a divided community," he said at the time--ignoring well-documented instances of segregation, including the "whites only" tree at Jena High.<p>
At that same meeting, Washington also denied that District Attorney Reed Walters had told Black students he could "end their lives with the stroke of a pen"--even though several Black students have come forward to say Walters was looking directly at them when he made the comments.<p>
With a "Justice" Department like this, it's no wonder that a CNN-Opinion Research Corp. poll released last month found that 79 percent of Blacks surveyed believed that the Jena 6 were treated unfairly.<p>

