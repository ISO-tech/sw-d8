<font face="Times New Roman, Times, serif" size="5"><b>Racist abuse ignites protest in upstate N.Y.</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 2, 2007 | Page 14</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>REBEKAH WARD tells how antiracists won a victory at a local school.</b><p>

<font face="Times New Roman, Times, serif" size="3">ITHACA, N.Y.--Racist harassment of a young girl at a local school and the school board's callous response to the abuse ignited a firestorm of protest and legal action here.<p>
While riding the bus to Dewitt Middle School, the young girl experienced the kind of racial harassment that should be a relic of another era. "We shoot n-----s like you in the woods," a group of boys riding the bus to Enfield, N.Y., told her. Her book bag was taken and spit upon, and when she later confronted the boy who took it, he punched her.<p>
Amelia Kearney, the girl's mother, approached the school board, the superintendent and the principal and vice principal at Dewitt about the issue, but she received little response and less action.<p>
After the punching incident, Kearney called the police and took her daughter out of school. Because the school could not (or would not) insure her daughter's safety, she asked them to provide private tutoring as per district policy, which they refused to do.<p>
Meanwhile, the harassment continued. While leaving the school bus, one of the boys involved in the harassment held up a sign reading, "KKK. I hate n-----s." Finally, two of the boys who had engaged in the harassment were arrested, while Kearney's daughter was granted an order of protection.<p>
But when Kearney's daughter entered high school this fall, she was placed in the same class with one of her attackers.<p>
This was the final straw for Kearney, who went to the county human rights commission (HRC) to file a lawsuit against the school district. The school district has resisted the suit, insisting that it falls outside the HRC's jurisdiction, and has sought a court injunction to halt any HRC intervention.<p>
Meanwhile, the community organized to demand that the school district take action against the racist harassment instead of fighting for the injunction. Protesters occupied the office of School District Superintendent Judith Pastel on October 2 and packed a recent school board meeting to demand that the district allow the HRC case to proceed.<p>
Students attempted to organize a district-wide forum on racism, but administrators banned their publicity materials and disrupted the meeting by giving excused absences to all students on the day of the forum. At an October 23 school board meeting, the district again faced an angry crowd of students, parents and community members--and finally caved to the pressure, dropping its pursuit of the injunction.<p>
It was a victory for those dedicated to fighting racism and a testament to the power of protest.<p>
But Pastel also announced a plan to place police officers in the high school on a regular basis, sparking new concerns for many community members.<p>
Activists are demanding the resignation of Superintendent Pastel, the architect of much of this disaster. If successful, it will send a clear message to the school district administration: Take opposing racism in our schools seriously, or you might be out of a job.<p>

