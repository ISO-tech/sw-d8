<font face="Times New Roman, Times, serif" size="5"><b>No gains in Teamsters new UPS contract</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Donny Schraffenberger, Teamsters Local 705</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 2, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">ACCORDING TO its Web site, UPS had a "very good year in 2006." But to judge from the proposed national contract, the company has, instead of rewarding its workforce, attempted to attack its part-time and combination full-time workers.<p>
According to CEO Mike Eschew, UPS "posted new highs in revenue, operating profit and earnings per share." Management boasts of its expansion around the globe, including construction of a new hub in Shanghai, China.<p>
UPS can certainly afford to raise pay and improve conditions. But the sad truth is that Teamsters negotiators let UPS dictate the terms of this contract.<p>
According to the union Web site, this proposed five-year contract is a victory. Teamsters President James Hoffa claimed that UPS wanted to eliminate 5,000 full-time combination jobs and destroy the full-time jobs of workers hired before the 1982 two-tier pay scale.<p>
Yet it's no victory to say we stopped UPS's fantasy of even deeper concessions. The company is making money hand over fist. We should be creating more full-time jobs, not claiming victory with zero. The 1997 national strike forced UPS to start creating better paying full-time positions.<p>
The majority of UPS workers are severely underpaid part-timers who are constantly harassed by management. Start pay is a meager $8.50 an hour and will stay at that rate until 2013. In 1981, UPS part-timers made the same wage as full-timers, then $12 an hour. In 1982, UPS cut starting pay 25 percent to $8 an hour. It has only gone up 50 cents since then!<p>
Meanwhile, top scale full-time package car and feeder driver pay is nearing $30 an hour. Yet part-timers still make only a third of that. No union can remain strong if most of its members are making poverty wages.<p>
Also, a union is about solidarity. The Teamster Central States pension plan united all Teamsters together from different companies. But Hoffa let UPS out of this pension program, which will hurt not only non-UPS Teamsters, but eventually UPS workers as well.<p>
Chicago Locals 705 and 710 have separate contracts with UPS, but still have no new proposed contract. Ballots for the national deal will be sent out October 26.<p>
We don't have to accept this terrible contract. Visit the <a href="http://www.tdu.org" target="_blank">Teamsters for a Democratic Union</a> Web site to download fliers explaining why this contract is a bad deal--and vote no!<p>

