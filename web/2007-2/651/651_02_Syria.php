<font face="Times New Roman, Times, serif" size="5"><b>The air strike you didn't hear about</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 2, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">IMAGINE IF the sides were reversed. A Syrian warplane streaks across the sky and bombs a site in Israel known to be used for the manufacture of nuclear weapons.<p>
If that had happened, Syria's capital of Damascus would be a mushroom cloud, courtesy of the Israeli military--and most likely the U.S. government as well.<p>
But on September 6, Israel carried out a "pre-emptive" strike against a Syrian nuclear facility that was years away from being able to produce weapons-grade material--and governments around the world, including the U.S., shrugged.<p>
Syria, as a signatory to the Nuclear Nonproliferation Treaty, is guaranteed the right to construct a nuclear facility for the purpose of generating electricity. According to the <i>New York Times, </i>it "would not have been obligated to declare the existence of a nuclear reactor during the early phases of construction."<p>
Yet Israel--using the same logic of pre-emptive war that George Bush employed to justify the invasion of Iraq--claimed the right to bomb the Syrian facility. It plainly violated the first principle of international law, but the strategic wisdom of Israel's act of aggression was nevertheless politely debated in Washington and beyond.<p>
The strongest criticism to be found in the <i>New York Times </i>report was this: "The Bush administration was divided at the time about the wisdom of Israel's strike, American officials said, and some senior policy makers still regard the attack as premature."<p>
It is a "public secret" that Israel possesses as many as 300 nuclear warheads, according to the <i>Washington Post</i>. Israel is one of a handful of nations--including U.S. allies Pakistan and India--to refuse to sign the Nuclear Nonproliferation Treaty.<p>
By its own logic, Israel's repeated and longstanding violation of a 1996 UN resolution establishing the Middle East as a "nuclear-weapon-free zone" should provide sufficient justification for bombing Israel's nuclear infrastructure.<p>
Mohamed ElBaradei, head of the International Atomic Energy Agency, expressed frustration that until Israel agrees to negotiate a halt to nuclear proliferation and itself disarms, "There will be continued incentive for the region's countries to develop weapons of mass destruction to match the Israeli arsenal."<p>
Israel's disarmament won't happen, though, in a world where might makes right--and the U.S. stands on the side of its ally when Israel carries out Bush-style pre-emptive strikes against rivals.<p>

