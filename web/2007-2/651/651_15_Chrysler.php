<font face="Times New Roman, Times, serif" size="5"><b>UAW rams through Chrysler deal</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 2, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>LEE SUSTAR reports on how union leaders sold another surrender to members at Chrysler.</b><p>

UNION LEADERS mounted an all-out effort to push through an unprecedented concessionary contract covering 45,000 workers at Chrysler by a narrow margin.<p>
The UAW's official final tally was 56 percent of production workers in support of the deal, and 51 percent of those in skilled trades voting "yes." However, a significant rank-and-file rebellion, coordinated by the Soldiers of Solidarity opposition network, nearly sank the deal.<p>
When two big United Auto Workers (UAW) locals in Kokomo, Ind., voted down the proposed deal, the contract appeared headed for defeat. But UAW officials took to the shop floor in Detroit-area plants to sell the deal with Chrysler's new owners, the private equity firm Cerberus.<p>
The contract even passed in the Sterling Heights, Mich., assembly plant, despite the opposition of local president Bill Parker, who also chaired the union's national bargaining committee with Chrysler.<p>
The closeness of the vote raised questions among opposition activists, who pointed out that contract ratification votes are handled differently in different locals. Even so, the vote reflects the fact that large numbers of Chrysler workers were unwilling to challenge both the UAW and the company by rejecting the deal.<p>
Like the UAW contract at GM ratified in September, the Chrysler deal creates a permanent lower-tier wage for future hires in "non-core" jobs off of the assembly lines, a category that, according to Chrysler, includes not only 19 parts warehouses but also three manufacturing plants.<p>
Also like GM, the contract allows Chrysler to turn over its retiree health care plan to a union-controlled Voluntary Employee Beneficiary Association (VEBA). According to <i>Automotive News,</i> Chrysler will spend $8.8 billion to rid itself of $16 billion in UAW retiree health care liabilities--that is, 55 cents on the dollar.<p>
To make up the difference, the UAW-VEBA trustees will have to beat the stock market and 10 percent annual inflation in health care--or cut benefits. The track record isn't good. Past VEBA plans accepted by the UAW at Detroit Diesel and Caterpillar have run out of money.<p>
There were other concessions in the Chrysler deal as well. New hires will get 401(k) plans instead of pensions and won't receive retiree health care. Annual lump-sum payments will replace wages, lowering the base rate of pay for future wages and benefits.<p>
Temporary workers--hired at GM as full-timers under the new contract--will remain temps. Specific commitments by management to investment in U.S. factories--a high-profile part of the GM deal--are absent in the Chrysler contract.<p>
Now, the UAW turns to negotiations with Ford, where executives will try special pleas for even more concessions. Ford will certainly demand a VEBA on terms similar to those at Chrysler and GM, as well as further downsizing to meet its goal of eliminating 30,000 jobs and closing at least six plants.<p>
While workers sacrifice, Ford CEO Alan Mulally got a $39.1 million in bonuses for "working" just the last four months of 2006, even as Ford lost $12.7 billion.<p>

