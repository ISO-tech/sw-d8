<font face="Times New Roman, Times, serif" size="5"><b>Kansas clinic targeted by anti-abortion prosecutor</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 2, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">OPPONENTS OF a woman's right to abortion have taken their crusade against reproductive health provider Planned Parenthood into the courts--and Kansas is ground zero for their assault.<p>
Johnson County District Attorney Phill Kline is charging a Planned Parenthood clinic in Overland Park, Kan., with 107 criminal counts--23 of them felonies. A total of 29 counts are for supposedly providing "unlawful late-term abortions."<p>
Kline is well known among anti-abortion forces in Kansas. He led a high-profile campaign against Planned Parenthood when he was state attorney general, and he was named "Man of the Year" in 2006 by anti-choice group Operation Rescue. In June, he delivered the keynote address at the National Right to Life Committee's convention in Kansas City, Mo, where he told the crowd, "The soil of Kansas is stained red...Abortion is sin, and sin always begins with a lie."<p>
Peter Brownlie, the president and CEO of Planned Parenthood of Kansas and Mid-Missouri, told reporters that the facility doesn't perform abortions after the 22nd week of pregnancy.<p>
"Phill Kline continues to use his office to further his political ambition of making abortion illegal by using unethical tactics in his role as district attorney," Brownlie said. "No health care provider should be threatened with felony convictions simply because elected officials oppose legal abortion."<p>
Kansas is one of 40 states with a law banning late-term abortion procedures. As attorney general, Kline repeatedly targeted the Overland Park clinic and a clinic in Wichita operated by Dr. George Tiller. Kline tried for two years to get abortion records from the clinics, claiming he was attempt to unearth evidence that doctors were performing illegal abortions and weren't reporting incidents of child abuse.<p>
Abortion foes used the allegations against Planned Parenthood in Kansas in their request to Congress last week asking that lawmakers suspend funding to the women's health provider until the legal action has been resolved.<p>
Meanwhile, Kansas abortion rights opponents announced October 26 that they have collected the 3,500 signatures needed to petition to convene a grand jury to hear further allegations that the Overland Park clinic broke the law, including claims that it trafficked illegally in fetal tissue.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">While abortion opponents may not be making a frontal attack on all abortions in this instance, they are using every restrictive law at their disposal to effectively shut clinics down.<p>
For example, Kansas anti-abortion activists are also demanding that a grand jury determine whether the Overland Park clinic complies with state parental consent requirements, has enforced a required 24-hour waiting period before performing abortions, and follows the "required standard of care" in providing medical advice.<p>
In Wichita, a grand jury is scheduled to convene October 30 to investigate Dr. Tiller, who has been the target of opponents of women's rights for well over a decade--since Operation Rescue descended on his clinic in 1991 as part of a nationwide campaign. Tiller's lawyers have asked the Supreme Court for a delay.<p>
In Portland, Maine, the right wing is using laws meant to protect young people from being victimized to bar their access to needed contraception.<p>
The King Student Health Center has provided important reproductive health care, including condoms and testing for sexually transmitted diseases, to students in grades six to eight, aged 11 to 15, since it opened in 2000. But when it announced it would begin prescribing birth control this month, the health center immediately came under fire.<p>
Cumberland County District Attorney Stephanie Anderson accused the center of breaking the law for failing to report to proper authorities that a child of 14 or under was having sex. Anderson cited a Maine law that prohibits having sex with a person under age 14, regardless of the age of the other person involved, or whether it is consensual.<p>
Currently, the city's six school-based health centers have no formal policy about reporting the sexual activity of students. Health officials are rightfully concerned that if strict rules on reporting are enforced, students will stop going to the centers completely.<p>
"If we're reporting to the district attorney's office, it brings it to a whole other level," said Douglas Gardner, director of Portland's Department of Health and Human Services. "I don't know what that would do to the comfort level of young people coming to us for health care."<p>

