<font face="Times New Roman, Times, serif" size="5"><b>Day of antiwar protest in major cities</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 2, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">THOUSANDS OF protesters in major cities, including New York, Boston, Chicago, Los Angeles and San Francisco, turned out for protests against the war in Iraq on October 27. The regional protests were initiated by United for Peace and Justice (UFPJ).<p>
Some 5,000 attended the protest in New York, where UFPJ's headquarters are located; 8,000 came out in Boston, some 8,000 in San Francisco, 7,000 in Chicago, 5,000 in LA and about 3,000 in Seattle.<p>
In New York City, despite the rain, hundreds of students marched in a lively campus antiwar contingent. Other contingents included Iraq Veterans Against the War (IVAW) and union members represented by Service Employees International Union, United Federation of Teachers Against the War and the CUNY's Professional Staff Congress.<p>
"Politicians who are running for president are saying we should pull troops out of Iraq and send them to other wars, better wars," said IVAW speaker Fernando Braga. "There are no better wars. This is a system of imperialism."<p>
In San Francisco, antiwar contingents included members of IVAW, Labor Against the War and the Campus Antiwar Network (CAN). The march concluded in a rally and concert featuring Cindy Sheehan, as seasoned activists joined with people just beginning to question the Iraq war.<p>
In Boston, author and activist Howard Zinn addressed the antiwar crowd in the Commons and, as he did in 1971 during the Vietnam War, demanded immediate withdrawal. "You can't have a war on terrorism; war is terrorism," Zinn said. "When enough soldiers refuse to fight, this war will not be able to go on, and we need to support them any way we can."<p>
The Boston protest was organized by a wide array of grassroots organizations in a coalition calling itself New England United, and over 200 groups from all over New England endorsed the protest.<p>
In Chicago, a significant portion of the people who attended the protest came from outside Illinois--Michigan, Iowa, Missouri and a whopping 16 buses from Wisconsin.<p>
In the run-up to the protest, some Chicago groups dropped their names from the endorsers' list in protest of organizers' insistence on issuing speaking invitations to Democratic politicians such as Chicago Mayor Richard Daley and Sen. Barack Obama (D-Ill.), who do not oppose the war.<p>
While these invited guests didn't attend, several other prominent local Democrats did, including Rep. Luis Gutierrez. Other speakers included Sheila Garland from the National Nurses Organizing Committee and Jorge Mujica of the March 10 Coalition, who criticized the military's policy of "green card soldiers," granting legalization only after they have served in the armed service, or have been killed.<p>
In LA, protesters marched to City Hall chanting "Iraq for Iraqis, troops out now!" and staged a "die-in." IVAW members were joined on the stage by Vietnam veteran Ron Kovic.<p>
In Seattle, marchers were led by a contingent of veterans in IVAW and Veterans for Peace. "It's time for the occupation to end and veterans should be at the head of the movement to end it," said IVAW Seattle chapter president Chanan Suarez Diaz.<p>
Some 25 members of the Seattle Education Association marched with their "Educators for Peace" banner and chanted "Money for schools and education, not for war and occupation."<p>
"It's so inspirational and exciting to see people from all over Washington, Oregon and beyond marching against the war," said David Barsamian from Alternative Radio and author of <i>Targeting Iran</i>. "We've got to keep pressure on the warlords in Washington before they launch another imperialist war on Iran."<p>
About 1,000 came out in Philadelphia, where protesters marched from the Veterans Administration Hospital in West Philly to Independence Mall. In New Orleans, 200 activists--some from Texas, Alabama and Georgia--marched through the French Quarter, chanting "Make levees, not war!"<p>
While protests were considerable smaller than previous national protests in several cities, such as San Francisco and New York, the attendance of large numbers of people for whom this was their first protest speaks to the untapped potential for organizing to demand U.S. troops out of Iraq.<p>
<i>John Green, Kurt Krueger, Matt Pillischer, Jen Roesch, Keith Rosenthal, Elizabeth Schulte, Matthew Tucker and Erik Wallenberg contributed to this report</i>.<p>

