<font face="Times New Roman, Times, serif" size="5"><b>ICE raid in LA foiled by protest</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By David Rapkin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 14, 2007 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">THE TYPICALLY arrogant agents of Immigration and Customs Enforcement (ICE) found themselves on the defensive in Los Angeles in the face of a small number of confident and determined community members who stood up to them early on September 6.<p>
After watching ICE stake out and begin to intimidate a close-knit South Central LA neighborhood at 6:30 a.m. with six vehicles and numerous agents, Cristina Hernandez, her family and friends succeeded in driving the agents away and saving the family that agents tried to apprehend and deport.<p>
As agents entered a private apartment in an attempt to arrest an undocumented family, Hernandez confronted other agents in the street. Already, she and her brother had moved around the block, photographing ICE vehicles. The agents demanded she and others show ID, and that she surrender her camera. Hernandez produced an ID, but refused to give up her camera.<p>
Her confidence split the agents, with one contradicting the other and admitting that it was indeed legal for people to take photographs. When the agents told Hernandez to stay inside and not take any more pictures, she responded defiantly, telling them, "I plan to go right in my house, make a poster and walk right back out with it, because what you are doing is wrong."<p>
Taken aback, they grudgingly conceded that she had a "constitutional right" to protest.<p>
Meanwhile, ICE agents had entered a nearby apartment and were preparing to arrest the occupants, who were unable to produce documents. But the commotion on the street, created by an increasingly confident Hernandez and her family, apparently caused the agents in the apartment to stop what they were doing and leave the premises. As soon as they left, the family escaped.<p>
"I don't think this was a simple mistake," Hernandez, a member of the International Socialist Organization, said later. "I think they realized they were actively being watched and decided to abort the arrest.<p>
"I think this proves the importance of our work. What they are doing is illegal, and they benefit from the fear they create in people to continue. I saw them break down when they realized that I would not back down."<p>
Unfortunately, a car from the LA County Sheriff's Department soon arrived in an obvious and illegal coordinated sweep. With ICE assistance, the sheriff's deputies arrested an African American man from the neighborhood.<p>
As soon as ICE drove away, Hernandez got on the phone and called her comrades and allies in the local Emergency Response Network (ERN), formed several months ago to allow activists to respond at a moment's notice to ICE raids and other anti-immigrant attacks.<p>
ERN activists quickly organized an afternoon protest and press conference, which drew five Spanish-language media outlets and about 25 activists and concerned neighbors.<p>
"The entire climate in the neighborhood changed from early in the morning to the afternoon," said Hernandez. "We need to continue building the ERN and other networks so that ICE trembles every time they go into one of our neighborhoods."<p>
The small but spirited afternoon protest was especially effective at drawing in African American neighbors, some of whom were ready to stand with their Latino neighbors at the protest. Many others expressed sympathy and outrage that a family had been attacked in their apartment for the "crime" of working and raising children.<p>
Sadly, the family was terrorized by the assault to the point that they are moving away from the community, even though they told Hernandez they have nowhere to go. They told Hernandez that when ICE was coming into their apartment, they called a brother, crying, and told him to "please take care of the baby because we're going to be deported."<p>
Yet the family was also "very, very thankful," according to Hernandez. "Although they are very terrified by the whole situation, they gained at least a little confidence by the media attention and our protest. They felt our presence created a safety net for them."<p>
All participants agreed that the difference made by Hernandez's quick action and the ERN protest later is the most important lesson from the day.<p>
"I learned that it is important to stand up against ICE," said Hernandez. "In this case it saved a family from deportation, but it was also important because it set a precedent. Communities can respond to ICE raids with courage instead of fear. So what we need are more of these types of responses in every neighborhood so that ICE can stay away from our families. People are tired of being victims."<p>

