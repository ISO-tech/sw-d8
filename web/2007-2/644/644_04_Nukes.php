<font face="Times New Roman, Times, serif" size="5"><b>Nukes on bomber flight over U.S.</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 14, 2007 | Pages 1 and 4</font><p>

<font face="Times New Roman, Times, serif" size="3">AN AIR Force bomber flew for three-and-a-half hours across the U.S. with six Cruise missiles, armed with nuclear warheads, loaded on its wings--all by mistake, according to the military.<p>
The B-52 was equipped with the missiles--each carrying up to 150-kilton warheads, with 10 times the destructive force of the atomic bomb dropped on Hiroshima--at Minot Air Force Base in North Dakota, and then flown to Barksdale Air Force Base in Louisiana.<p>
"You can wipe out a good-sized city with a 5-kiloton blast," a Greenpeace analyst told the <i>Chicago Tribune</i>.<p>
Backpedaling after the story emerged when three officers leaked details to the <i>Military Times </i>newspapers, Pentagon officials insisted there was never any danger. "At no time was there a threat to public safety," said Lt. Col. Ed Thomas said.<p>
Perhaps the Midwesterners who live along the 1,000-mile-plus flight path disagree.<p>
Another Air Force spokesperson declared, "The weapons were always in our custody"--apparently to draw attention to the fact that they at least weren't stolen in midair during their journey across the U.S.<p>
Coming amid U.S. officials' frantic warnings about Iran developing nuclear weapons--not to mention their lectures to Vladimir Putin to keep a closer watch on Russia nuclear weapons and materials--the flight of the nuclear-armed B-52 was a major embarrassment for Defense Secretary Robert Gates and his boss, George Bush.<p>
The incident was also a stark reminder from a frightening chapter in U.S. history--the Cold War with the former USSR, when it was U.S. military policy to keep nuclear-armed planes in the air at all times.<p>
That policy changed in the late 1960s after a series of accidents, including a midair collision that spread radioactive contamination across a part of the countryside in Spain. But until 1991, bombers armed with weapons remained poised on runways, ready for a quick takeoff. After the collapse of the former USSR, George H.W. Bush ordered all nukes to be removed from aircraft and kept in storage facilities.<p>
But the U.S. military's nuclear arsenal remains the largest in the world, by far. The U.S. has nearly 10,000 warheads, with a significant portion ready to be launched within minutes.<p>
In contrast to the dominant image of nuclear weapons waiting in silos, many new warheads are designed for more mobile delivery systems--like the Cruise missiles attached to the wings of B-52s.<p>
The military insists--and former Air Force personnel, commenting on the Internet, confirmed--that there is a labyrinth of safeguards to stop a B-52 from being loaded with nukes. "I just can't imagine how all of this happened," said Philip Coyle, an analyst at the Center for Defense Information. "The procedures are so rigid; this is the last thing that's supposed to happen."<p>
Thus, many questions continue to loom over the incident. One of the scariest is why the warheads were ever taken out of storage in the first place, even if they were supposed to be moved by the military's accepted procedure of carrying them on cargo planes.<p>
If the weapons were slated to be decommissioned, as some in the media suggested, they would normally be sent to Kirtland Air Force Base in New Mexico. Instead, the nukes ended up at Barksdale--which just happens to be the chief base for launching long-range air attacks against the Middle East.<p>
Cruise missiles with conventional warheads were loaded onto planes at Barksdale to strike targets in Iraq during Bill Clinton's air attacks of the 1990s, and the Operation Shock and Awe bombardment ordered by George Bush in 2003.<p>
However the six warheads made it onto the B-52, the U.S. government's nuclear hypocrisy has been revealed again.<p>

