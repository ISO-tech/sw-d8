<font face="Times New Roman, Times, serif" size="5"><b>"Home is cage, and cage is steel"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 14, 2007 | Page 11</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i><a href="http://www.amazon.com/gp/redirect.html?ie=UTF8&amp;location=http%3A%2F%2Fwww.amazon.com%2FPoems-Guantanamo-Detainees-Ariel-Dorfman%2Fdp%2F1587296063%3Fie%3DUTF8%26s%3Dbooks%26qid%3D1189538618%26sr%3D8-1&amp;tag=socialistwork-20&amp;linkCode=ur2&amp;camp=1789&amp;creative=9325" target="_blank">Poems from Guant&aacute;namo: The Detainees Speak</a>, </b></i>edited by Marc Falkoff. University of Iowa Press, 2007, 72 pages.</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>KEITH ROSENTHAL reviews a powerful collection of poems by Guant&aacute;namo Bay detainees.</b><p>

WITH NOTHING at his disposal but a Styrofoam cup and a pebble, the celebrated Pakistani poet Shaik Abdurraheem Muslim Dost wrote poems that passed from cell to cell, lifting the hearts of the prisoners who read its lines.<p>
He wrote in the voice of a son of a detainee:<p>

<blockquote>
Eid [Muslim feast] has come, but my father has not<br>
He is not come from Cuba.<br>
I am eating the bread of Eid with my tears.<br>
I have nothing.<br>
Why am I deprived of the love of my father?<br>
Why am I so oppressed?
</blockquote><p>

Dost was released from the now-notorious, high-security U.S. prison in Guant&aacute;namo Bay, Cuba, in 2005 without being charged with anything. For four hellish years, he languished in what has become a void in the world of international law.<p>
The first prisoners of the U.S. "war on terror" were brought to the Guant&aacute;namo prison in 2002. Since then, more than 750 Muslim men and boys (ranging in ages from 14 to 80) from 40 different countries have passed through the prison camp.<p>
Classified as "enemy combatants"--rather than "prisoners of war," which would confer upon them basic rights under the Geneva Conventions--the inmates of Guant&aacute;namo live in a legal limbo. They have no right to challenge their detentions in court; they have no legal protections against torture in interrogations; they aren't allowed to know the crime of which they are accused; their cases are heard in secret military tribunals without legal counsel; and they can be held indefinitely without cause.<p>
President George Bush justifies their detention by claiming they are "the worst of the worst." Yet, in the five years of the camp's existence, only 10 detainees have been charged with any crime, 395 have been released and, according to the military's admission, over half are not suspected of committing any violent acts whatsoever.<p>
For five years, these men have been prevented from uttering even a peep that could be heard by the world outside the prison camp. Until now.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">IN <i>POEMS from Guant&aacute;namo</i>, the poetry of 17 different inmates have been collected and published for the world to read. They have been translated from their original Arabic and represent the select few poems that made it through a grueling Department of Defense screening, in which hundreds were censored.<p>
Originally, the works in the collection were written by inmates for one other, for themselves or for their lawyers. Most of them never supposed their poetry would ever reach a wider audience, as many of their poems were confiscated by prison guards and destroyed. As the poets would later explain, they wrote simply to maintain their sanity in otherwise desperate conditions.<p>
Starting from this isolated framework, the poetry has a personal, almost confessional, intimacy and beauty. Ranging a broad spectrum of tone and content, the poems display the true variety of humanity imprisoned at Guant&aacute;namo.<p>
Some of the poems find solace in deeply held religious conviction. Others express the heightened emotional state of intense loneliness, homesickness, fear and hope. Others rage at U.S. hypocrisy and the oppression of injustice.<p>
One of the poets, Moazzam Begg, was one of a number of British citizens held in Guant&aacute;namo. He spent three years there before being released in 2005. In his poem, "Homeward Bound," he expresses the feeling that all detainees face in trying to cope with the reality of their imprisonment: "Freedom is spent, time is up--/Tears have rent my sorrow's cup;/Home is cage, and cage is steel,/Thus manifest reality's unreal."<p>
Jumah al Dossari is a Bahraini citizen who has been held at Guant&aacute;namo for five years. Four of those years were spent in solitary confinement. He has attempted suicide 12 times. He writes: <p>

<blockquote>
Take my blood.<br>
Take my death shroud and<br>
The remnants of my body.<br>
Take photographs of my corpse at the grave, lonely.<br>
Send them to the world,<br>
To the judges and<br>
To the people of conscience,<br>
Send them to the principled men and the fair-minded.<br>
And let them bear the guilty burden, before the world,<br>
Of this innocent soul.
</blockquote><p>

After long periods of seclusion and powerlessness, it's natural for a person to begin to assign human agency to nature or inanimate objects. In one of the more beautiful poems in the collection, Ibrahim al Rubaish attempts to win the sympathy of the sea away from his captors:<p>

<blockquote>
O Sea, do our chains offend you?<br>
It is only under compulsion that we daily come and go.<br>
Do you know our sins?<br>
Do you understand we were cast into this gloom?<br>
O Sea, you taunt us in our captivity.<br>
You have colluded with our enemies and you cruelly guard us.<br>
Don't the rocks tell you of the crimes committed in their midst?<br>
Doesn't Cuba, the vanquished, translate its stories for you?<br>
You have been beside us for three years, and what have you gained?<br>
Boats of poetry on the sea; a buried flame in a burning heart.
</blockquote><p>

Some critics have questioned the extent to which this collection of poems constitutes "art" as opposed to "propaganda." But one need not do more than open the book to any random page and read the chance selection to see such accusations as baseless prejudice.<p>
Nonetheless, this work is inherently more than art. It is unavoidably political in that it expresses the humanity of a group of people who the most powerful government in the world wants to be depicted as anything but.<p>
It is in this regard that the editor of the anthology, Marc Falkoff, says of the detainees' writings, "perhaps their poems will prick the conscience of a nation." We should only hope so.<p>

