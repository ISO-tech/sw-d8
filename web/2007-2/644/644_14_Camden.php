<font face="Times New Roman, Times, serif" size="5"><b>Workers win at Camden Yards</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 14, 2007 | Page 14</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>ALANA SMITH reports on the struggle of stadium workers in Baltimore for a living wage.</b><p>

BALTIMORE--The United Workers Association (UWA) celebrated a victory last week in their three-year-long fight for a living wage for Camden Yards stadium cleaners.<p>
The UWA has been ramping up pressure on the Maryland Stadium Authority (MSA) over the past several months, and some of its members had planned to begin a hunger strike if a September 1 deadline for granting a living wage hadn't met.<p>
Workers currently earn $7 an hour, which is higher than minimum wage. However, the official living wage for Baltimore is $9.62.<p>
In addition, they are forced to pay transportation fees--a van ride from the temp agency is required, even if a worker lives within walking distance of the stadium--equipment rental fees and check-cashing fees, which means they take home much less than $7 an hour.<p>
These day laborers, who are primarily Latino and Black, also face awful working conditions. Breaks are often denied, women are sexually harassed, and workers have to eat their lunch in the restrooms.<p>
The UWA has taken inspiration directly from the immigrants' right struggle, consciously modeling their approach on that of the Immokalee workers in Florida and using a quote from C&eacute;sar Ch&aacute;vez--"We Hunger for Justice"--as a slogan.<p>
The UWA held a prayer breakfast on Labor Day, which was attended by Mayor Sheila Dixon, and a candlelight vigil on September 6. The vigil turned into a victory celebration when word came that the MSA had agreed to include the living wage as a requirement of temp agencies who bid for the contract when it is renewed next year.<p>
The state's new living-wage law, which goes into effect October 1, requires government contractors to pay their employees $11.30 an hour in the Baltimore-Washington corridor, and $8.50 in the rest of the state. The law only applies to employees who work 13 consecutive weeks over the course of a contract, so it doesn't cover stadium cleaners, who only work during home games.<p>
But MSA chair Frederick Puddester said that he supports a living wage for the cleaners and recommended to the board that the higher wage be specified in a request for proposals from vendors seeking to bid on the cleaning services.<p>
On September 8, a concert planned to kick off the hunger strike instead celebrated the victory. It featured Washington Wizards center and political poet Etan Thomas; local hip-hop acts Sean Toure, Son of Nun and Head-Roc; and 14-piece Afro beat/rumba band Chopteeth. About 150 supporters gathered to hear music and hear speakers, which included Ricardo Juarez from Mexicans Without Borders and a representative of the Coalition of Immokalee workers.<p>
The next priority for the UWA is ensuring that stadium workers will get a fair opportunity to keep their jobs next season and work at a living wage.<p>

