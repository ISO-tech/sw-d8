<font face="Times New Roman, Times, serif" size="5"><b>A legal setback for "no-match" firings</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 14, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">A JUDGE has taken away--for now--the Bush administration's latest weapon for hunting down undocumented workers. But the federal government is determined to step up its campaign of raids and deportations anyway.<p>
A federal judge ruled August 30 that the Department of Homeland Security could not require employers to use discrepancies in workers' Social Security numbers as grounds for termination.<p>
Already, a growing number of immigrant workers have been fired as a result of so-called "no-match letters"--which refers to the fact that the Social Security numbers on file don't correspond with those given by workers.<p>
Had it gone into effect, the new policy would have required employers to give workers 90 days to clear up the discrepancy--if workers failed to do so, the employer would have to terminate them or face stiff fines.<p>
But in a lawsuit challenging the policy, the AFL-CIO, the ACLU and other pro-immigrant organizations pointed out that the Social Security Administration itself admits that of 17.8 million people with incorrect Social Security numbers, 12.7 million were native-born citizens.<p>
The reasons for such errors are typically clerical mistakes and name changes due to marriage. A judge agreed that such problems warranted an injunction against the new policy, with hearings on a permanent ruling to begin October 1.<p>
Nevertheless, the stepped-up raids and deportations of undocumented immigrants continues--and is getting worse. Immigration and Customs Enforcement (ICE) has escalated its campaign of arrests in workplaces and communities around the U.S.<p>
There has been resistance, however. For example, in Los Angeles, members of an emergency response network were able to mobilize protests against ICE on September 6, allowing those targeted to avoid arrest.<p>
Opposition to the no-match policy is also building in advance of the October 1 hearings. The Chicago Committee Against the No Match Letters will launch a series of actions with a press conference September 12 at the Social Security office and a protest at the local ICE headquarters. Labor, community groups, faith organizations and others are involved.<p>
With the debate on immigration reform legislation on hold for the foreseeable future, the immigrant rights movement will need to focus its attention on these kinds of initiatives.<p>

