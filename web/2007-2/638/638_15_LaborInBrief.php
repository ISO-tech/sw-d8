<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">July 20, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Orange County bus drivers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Gillian Russom and Randy Childs</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SANTA ANA, Calif.--About 1,100 bus drivers from the Orange County Transportation Authority (OCTA) went on strike July 7 in a fight for better wages and pensions, shutting down about 50 of 81 routes.<p>
After just over a week on the picket lines, the members of Teamsters Local 952 voted by a margin of 696 to 35 for a new three-year contract that will give raises ranging from 10.8 percent for new hires to 11.7 percent for veteran drivers.<p>
As <i>Socialist Worker</i> went to press, it appeared that the union had also won improvements to their medical benefits and OCTA's agreement to continue the practice of allowing leftover employee medical insurance money to augment their pensions.<p>
Local 952 Secretary-Treasurer Patrick Kelly explained to the <i>Los Angeles Times</i> that with the most senior drivers earning between $45,000 and $60,000 per year, many qualify for housing assistance in a county where the median home price is $635,000--the highest in Southern California. At least half of OCTA drivers can't afford to live there.<p>
The new deal will give drivers with fewer than five years' experience starting pay of $14.20 per hour, rising to $15.22 by the end of the contract. Veteran drivers' pay will rise from $22.33 per hour to $23.92.<p>
The issue of how raises should be distributed was a sticking point in negotiations. Tammy, a driver for one year, explained, "[OCTA CEO Art Leahy] is trying to drive a wedge between newer and older workers so new workers cross the picket line. In the last contract, the old drivers took a pay cut. So what he's offering them now is what they gave up before--it's not a raise. They deserve every penny of what they're asking for."<p>
The union faced many challenges: a "cooling-off period" called by Gov. Arnold Schwarzenegger, which delayed the strike for two months; hostility from local media and OCTA board members; and the hiring of replacement workers and scabbing by supervisors.<p>
Los Angeles and Orange County media focused exclusively on the hardship that the strike caused to Orange County's working poor. But as striker Scott Watt said in an interview, "Unions evolved out of the people who were being oppressed and excluded when immigrants came over and built this country. If they didn't exist, we wouldn't have a middle-class living standard. Striking is our only leverage."<p>
Union leader Kelly called the strike "a bittersweet victory." The union clearly felt the bosses' pressure not to "inconvenience the riders" and could have won more had they been prepared to counter management's tactics.<p>
But as Kelly pointed out, something else was gained. ""It wasn't just about money," he said. "They weren't taking us seriously. They just took us for granted until we started making noise."<p>

