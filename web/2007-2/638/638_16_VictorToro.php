<font face="Times New Roman, Times, serif" size="5"><b>Don't let ICE deport V&iacute;ctor Toro</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">July 20, 2007 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>ORLANDO SEP&Uacute;LVEDA reports on the arrest of a leading immigrant rights activist.</b><p>

<font face="Times New Roman, Times, serif" size="3">THE BUREAU of Immigration and Customs Enforcement (ICE) detained a prominent immigrant rights activist and is preparing to deport him.<p>
V&iacute;ctor Toro, a left-wing Chilean who fled the military dictatorship of Gen. Augusto Pinochet and who has become a leading figure defending immigrants and the homeless in New York City, was arrested in July on board an Amtrak train. He is out on bail but is still facing a deportation order.<p>
Toro was returning home from a meeting in Rochester, N.Y., where participants discussed the failure a proposed immigration law in Congress, and what the movement should do next.<p>
"It's not time for weeping," Toro said. "What remains for us is the path of struggle and bottom-up organization throughout the country. They arrest us day and night, and deport us, and Congress puts us down, seeking to pass racist and criminal immigration laws that openly violate our civil, human and labor rights."<p>
ICE agents boarded the Amtrak train suddenly and arrested everybody who didn't look like a citizen and couldn't produce papers. Thirty-five people were arrested along with Toro. An ICE spokeperson said that such checks are becoming a more and more frequent routine.<p>

