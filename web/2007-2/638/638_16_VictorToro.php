<font face="Times New Roman, Times, serif" size="3">In Chile, Toro was a member of Movimiento de Izquierda Revolucionario starting from the late 1960s. He became a leader of the homeless and landless movement, organizing the occupation of idle land, which allowed thousands of families in Chile a place to build homes or land to farm.<p>
Because of his role in the land occupations and his prominence as a popular leader during the presidency of socialist Salvador Allende, Toro "became one of the most hated people by the Chilean bourgeoisie," said Mario Venegas, a Chilean exile living in Chicago, who was part of a solidarity fundraiser for Toro last week.<p>
When Pinochet carried out his coup against Allende on September 11, 1973--backed by Chile's rich and the war criminals of the U.S. government--inaugurating a reign of terror against the Chilean working class and leftists, Toro appeared in the press as one of the 13 people most wanted by the military.<p>
He was arrested and thrown into four different detention camps, where he was tortured numerous times, including with the application of electricity to his genitals. He was finally expelled from the country in 1976, and his death was later reported by the government.<p>
In reality, Toro wandered to numerous countries seeking refuge, but no government would allow him to stay. In 1984, he crossed the border from Mexico into the U.S.<p>
Here, he has continued in the struggle of the homeless for the right to a place to live, leading numerous occupations of abandoned buildings in New York City. In 1997, when police raided one of these buildings, where a group of 48 families had lived for 11 years, Toro responded by occupying the famed St. Patrick's Cathedral in the heart of Manhattan.<p>
To centralize his work with the homeless, Toro and his wife Nieves founded La Pe&ntilde;a del Bronx, a place which offers help to everybody who needs it, with no questions asked nor papers demanded.<p>
Now Toro's work on behalf of the homeless and immigrants is being threatened by ICE's deportation order. But Toro is undeterred. "We are staying," he said. "We have nothing to lose. Only the struggle will make us free and give us dignity."<p>

