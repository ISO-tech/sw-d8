<font face="Times New Roman, Times, serif" size="5"><b>The two systems of U.S. "justice"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">July 20, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE SENTENCE was "excessive" and should be commuted. That statement could be applied to thousands of men and women currently sitting on death row in the U.S.<p>
But instead, George Bush decided that 30 months in prison for Lewis "Scooter" Libby--Dick Cheney's former chief of staff, convicted of perjury and obstruction of justice for his role in "outing" CIA agent Valerie Plame--deserved mercy. A fine, probation and damage to Libby's reputation was surely punishment enough, Bush said.<p>
So much for Bush's claim in 1999--used to justify his 152 executions as governor of Texas--that "I don't believe my role is to replace the verdict of a jury with my own."<p>
But you can bet Bush won't have a word to say about "excessive" sentences in the cases of Troy Davis in Georgia and Kenneth Foster in Texas--both innocent men facing execution in the coming weeks.<p>
In Davis' case, seven of the nine original witnesses against him have recanted or contradicted their testimony. Several said they had been coerced by police. Three other witnesses who didn't testify at the trial say they saw another man commit the crime.<p>
But because of a legal technicality--the witnesses recanted too late to have their new statements allowed into evidence because of time limits enacted under Bill Clinton's 1996 Anti-terrorism and Effective Death Penalty Act--Troy Davis could still die.<p>
As <i>Socialist Worker</i> went to press, the Georgia State Board of Pardons and Paroles granted Davis a last-minute stay of execution--but only after the case gained national attention as an outrageous miscarriage of justice.<p>
As for Kenneth Foster, he now faces execution for driving a car. Foster was sentenced to death under Texas' "Law of Parties" for the 1996 murder of Michael LaHood.<p>
Prosecutors admit that Foster was 80 feet away, sitting in a car, when Mauricio Brown, who was executed in July 2006, shot LaHood. Brown said Foster didn't know he had left the car with the gun. But under the law of parties, anyone present at the scene of a crime can be held legally responsible, even if they had nothing to do with the crime itself. Texas is scheduled to execute Foster on August 30.<p>
The double standard is clear: It's fine for politicians to step in to protect one of their own from spending a single day behind bars--but they don't think the life of an innocent man on death row is worth living a finger to save.<p>
We have to organize to prove them wrong.<p>

