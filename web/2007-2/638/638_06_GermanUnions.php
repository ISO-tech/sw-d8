<font face="Times New Roman, Times, serif" size="5"><b>The fight facing German workers</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">July 20, 2007 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">VERDI, GERMANY'S second-biggest trade union, was dealt a bitter blow a few weeks ago when it caved in to Telekom management after more than five weeks of strikes.<p>
Telekom is the largest European telecommunications company and a spin-off of the former state-owned Deutsche Bundespost after its privatization in the mid-1990s. Verdi was born out of a fusion of five independent trade unions in 2001 and currently has about 2.3 million members. Its primary focus is on public-sector service workers, but media workers as well as the banking and insurance sector are also covered.<p>
At the heart of the struggle is a major reorganization that will affect some 50,000 employees. The plan is to reshuffle them into smaller "service companies."<p>
The reason is the same heard from companies the world over: "Due to the pressure of globalization we'll have to get leaner and meaner if we want to remain competitive." The results for the average worker are as expected, too: More work for less pay.<p>
Verdi still claims to have reached an agreement that won't result in less pay. The reality of the deal, however, looks quite different. It is true that Telekom will make up the difference in pay for those who end up in the new "service companies" and keep them 100 percent whole. However, this only holds true for the first 18 months.<p>
In 2009, the employees' wages will be reduced by 2.2 percent and in 2010, by 4.3 percent. Telekom will stop all "make up" payments at the end of 2010, so that from 2011 on, the pay cut will be at 6.5 percent. At the same time, the workweek goes up from 34 to 38 hours and overtime will no longer be paid for Saturdays. This results in a loss of approximately 11 percent when converted into hourly wages.<p>
Even more alarming is that this compromise could serve as a blueprint for docking pay in other former public sector industries. In this context, the ongoing battle over the privatization of Germany's railroad sector (officially privatized in the mid-1990s as well, but 100 percent of the stock is still owned by the Federal Republic of Germany) and the current train engineer strike is crucial.<p>
Germany's train engineers have their own union and are against a privatization of Deutsche Bahn, while Germany's railroad sector unions Transnet and GDBA are pro-privatization. More battles are also ahead in the mail sector of former Deutsche Bundespost, where management's promise in the last agreement (to not sack workers for economic reasons) will expire in March 2008, and layoffs are to be expected.<p>
Ultimately, this shows the dilemma of reformist trade unions working within a capitalist framework. Even when operating with arguably good intentions, they are vulnerable to be blackmailed into undesirable outcomes. In the process, improvements that took decades of struggle to implement are given up in a few rounds of "negotiations."<p>
In the final analysis, it is only the power of the working class itself not trade union bureaucrats that can bring about lasting and fundamental change.<br>
<b>Folko Mueller</b>, Houston, Texas<p>

