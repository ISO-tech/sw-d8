<font face="Times New Roman, Times, serif" size="5"><b>Dominican Republic hit by general strike</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Emmanuel Santos</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 20, 2007 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">A 24-hour general strike in the Dominican Republic July 9 partially halted public transportation and commerce in a protest against the neoliberal policies of President Leonel Fern&aacute;ndez and his administration.<p>
The "First General Strike for the Right to Live" was organized by the Alternative Social Forum (FSA), which is comprised of 200 grassroots, leftist and trade union organizations. It demanded lower food and medicine prices; an increase in salaries, including for members of the military and the police; an end to housing evictions; plus more structural demands, such as a new hydrocarbon law to quell rising gas prices.<p>
Schools and stores were mostly closed. The exception was malls and supermarkets for the rich, zealously guarded by soldiers. But the chief of police failed to convince other storeowners to open their doors.<p>
The FSA became the target of a propaganda campaign by the government, which threatened prosecutions if any acts of vandalism broke out. A countrywide witch-hunt against left-wing and labor activists resulted in the arrest of 50 people.<p>
When five youths blew themselves up while making homemade bombs, the government tried to blame the FSA--which immediately repudiated any form of isolated, violent attacks carried out in the name of the popular movement. The injured were members of Struggle, Unity and Progress Front (FLUP), a small far-left organization.<p>
In the northwestern city of Navarrete, a former activist was murdered in what seemed an attempt to provoke a violent reaction from a community that has seen some of the most militant protests in the past--and where the left-wing autonomist group, Broad Front of Popular Struggle (FALPO), is rooted.<p>
In the Santiago province, a women's collective holding a meeting was practically held hostage by heavily armed soldiers.<p>
Army units patrolled the streets of deserted major cities and poor neighborhoods while helicopters hovered in the sky. But this didn't deter peaceful marchers in the Capotillo neighborhood in Santo Domingo, the capital.<p>
Chanting "Para qu&eacute; avi&oacute;n, si hay hambre por mont&oacute;n!" ("Why planes, when there's plenty of hunger")--a reference to the recent purchase of nine Super Tucano planes from Brazil at a cost of $40 million--the protesters remained united in defiance of the hundreds of soldiers flanking their demonstration.<p>
FSA spokesperson Victor Jeronimo characterized the strike as a "resounding popular referendum" that rejected the social and economic policies of the regime.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">THE GENERAL strike came after a resurgence of protest in March--and a week after a combative popular movement in the working class community of Pedro Brand in Santo Domingo staged a two-day strike that paralyzed one of the most important highways in the country to protest the lack of basic services, such as water and electricity.<p>
As protesters burned tires to block the highway, police and SWAT teams appeared on the scene. Tensions flared after a high-ranking police officer ordered the arrest of all Afro-Dominicans. But this attempt to racially profile and criminalize protesters backfired as chants got louder and a sense of collective unity was forged.<p>
Within days, the government removed the officers in charge of this operation as a punishment for not firing on the demonstrators. But with business and tourist activity between the capital and the Cibao region disrupted--and millions in toll revenues going uncollected--the government sent out 80 trucks to distribute water.<p>
The victory in Pedro Brand and the recent general strike illustrate the power that ordinary people in the Dominican Republic still have.<p>
On July 28, the FSA will discuss the way forward. It is important for the movement to broaden the resistance by linking up with organized labor as a whole, and take up on the plight of undocumented Haitian immigrants, whose cheap wages are justified to lower the living standards of everyone.<p>

