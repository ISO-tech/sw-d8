<font face="Times New Roman, Times, serif" size="5"><b>Veterans and staff take on the VA</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">July 20, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>CHRIS MOBLEY and DARRIN HOOP report on Seattle antiwar activists' planned protest for veterans' rights.</b><p>

SEATTLE--Antiwar activists are preparing for a July 28 protest at a Seattle Veterans Administration (VA) hospital to demand more money for veterans' care and an immediate end to the war.<p>
The Troops Home Now Coalition will be joined by members of Iraq Veterans Against the War (IVAW); members of the American Federation of Government Employees (AFGE) Local 3197, which represents workers at the VA Puget Sound Health Care System Seattle Division; military families; and many others.<p>
Demands include an immediate end to the Iraq war, full funding of the Department of Veterans Affairs, separating VA funding from war funding, and meeting the demands of AFGE members at the VA hospital--which include increased staffing, a better nurse- and staff-to-patient ratio, and extended bargaining rights to include doctors' hours, nurses' assignments and staffing.<p>
Nationwide, a returning soldier has to wait an average of 165 days for a VA decision on his or her initial disability benefits, and an appeal can take up to three years according to the California Nurses Association.<p>

