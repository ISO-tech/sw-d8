<font face="Times New Roman, Times, serif" size="3">In the first quarter of 2006, the Department of Veterans Affairs treated 20,638 Iraq veterans for post-traumatic stress disorder. They have a backlog of 400,000 cases.<p>
The waiting list for VA medical care in the Puget Sound region is as high as 3,000 claims. As AFGE steward Steve McFadden said in an interview, "There has been a lot of short staffing in the hospital, and that's an area we're very concerned about. There's a huge claims backlog in the VA, and that comes about by not having enough people to process claims."<p>
In May, the Seattle VA was threatened with losing accreditation for its mental health unit after it was found not meeting requirements for patient and staff safety.<p>
Recently, Sen. Patty Murray (D-Wash.) visited the hospital to inspect the facility--and incredibly declared that the VA was taking the necessary steps to improve its mental health unit. Yet only hours before her visit, a patient tried to kill himself by slashing his arms with glass punched out from the window of an isolation room. This followed another suicide at the American Lake facility, located south of Seattle.<p>
It took McFadden contacting the local press for Murray's office to even respond to the incident. Now, the hospital is replacing the windows with shatterproof glass.<p>
These are just a few of the difficulties facing medical staff within the VA system locally. Nationally, Congress currently is debating whether to increase the budget of the Department of Veterans Affairs to $67 billion and add 1,000 new staff to the system.<p>
As Chanan Suarez-Diaz, president of the Seattle chapter of IVAW, pointed out, "This government can't be serious when they declare that they support the troops when they've been cutting VA benefits. Cutting benefits completely undermines the care that these veterans deserve after being deceived into a war [and] occupation that has ruined their lives forever."<p>

