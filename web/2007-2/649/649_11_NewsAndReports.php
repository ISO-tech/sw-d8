<p><a name="Rights"></a><font face="Times New Roman, Times, serif" size="4"><b>Immigrant rights in Northern Virginia</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By David Thurston</b></font><p>

<font face="Times New Roman, Times, serif" size="3">HERNDON, Va.--Many immigrants in Prince William County in northern Virginia took the day off work October 9 to oppose an anti-immigrant resolution passed by the Board of Supervisors in July. The strike was the third in a series of actions voted on at mass meetings organized after the resolution was passed.<p>
If it takes effect, the resolution would strip undocumented immigrants of all access to public services, including schools and hospitals. It would also authorize police to inquire about immigration status as part of routine police business, and to turn undocumented residents over to federal immigration authorities.<p>
On October 9, hundreds of workers gathered in the afternoon for a caravan around the county. Pro-immigrant slogans were painted on cars with shoe polish, and several hundred cars set out on a 10-mile drive across the county. This was followed by a rally at the county's Government Center, where a crowd of over 500 gathered to protest.<p>
Ricardo Juarez, a leader of Mexicanos Sin Fronteras, the primary organization leading the protests, estimated that around 3,000 workers took part in the one-day strike, although the numbers involved in the day's public actions were smaller.<p>
Right now, the fate of the resolution is unclear. The Board of Supervisors has voted to approve it, but has not yet voted to fund it--and it recently postponed a vote on this question to October 16. The policing component alone is projected to cost the county some $14 million.<p>
Additionally, a coalition of local groups recently filed a lawsuit claiming that the resolution is unconstitutional. This lawsuit could delay implementation of the resolution or eventually see it overturned.<p>
Meanwhile, the attempt to use economic leverage to advance the local battle for immigrant rights was a first for the Washington metropolitan area. The course of this struggle will be critical to the fate of immigrant workers across the region.<p>

