<a name="Match"></a><font face="Times New Roman, Times, serif" size="4"><b>Day of action against no match</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Rick Greenblatt</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN DIEGO--"Do you want to break up families, fire productive workers, and remove an estimated $7 billion from the U.S. economy?" These were the questions posed at a rally and press conference held outside the office of the Social Security Administration here October 12.<p>
Some 80 immigrant rights activists attended the noontime event, which was sponsored by a coalition of immigrants' rights, religious groups and trade unionists, including Chula Vista ACORN, the S&iacute; Se Puede Coalition, the Interfaith Coalition for Immigrant Rights, the American Friends Service Committee, the Center for Social Advocacy and United Domestic Workers-AFSCME. Churches in both San Diego and Tijuana also endorsed it.<p>
The event was part of a national day of action against new immigration measures announced by the Department of Homeland Security last month. Thousands came out in cities across the country, including Milwaukee and Los Angeles.<p>
In San Diego, activists used the protest to publicize a joint letter that the coalition will send to the Social Security Administration demanding a halt to "no match" letters. The letters, which have been blocked temporarily by a federal court ruling, would require employers to fire workers without valid Social Security numbers.<p>
The coalition also announced that it established a toll-free number--1-877-NOMATCH (1-877-666-2824)--that workers and family members can call to seek assistance with no match problems in the San Diego area.<p>
Several speakers, including Brookes Ashby of United Domestic Workers-AFSCME, pointed to the contributions that immigrants make to the U.S. economy. Enrique Morones of the Border Angles pointed to the recently approved law that prevents California landlords from inquiring about renters' immigration status as a victory. Avery Wear of the S&iacute; Se Puede Coalition raised the slogan "Una soluci&oacute;n, legalizaci&oacute;n" (One solution, legalization), which was taken up as a chant.<p>
About 10 counter-protestors from the San Diego Minutemen unsuccessfully attempted to shout down the speakers.<p>
