<font face="Times New Roman, Times, serif" size="5"><b>Raids drive immigrants from jobs at Smithfield</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 19, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">FACED WITH stepped-up raids and arrests, immigrant workers at the massive hog-processing plant operated by Smithfield Foods in Tar Heel, N.C., are fleeing.<p>
Since last November when federal officials began their crackdown, some 1,100 Latino workers have left the plant, fearing that if they stayed, they would be exposed to imminent arrest and deportation.<p>
Maritza Cruz told the <i>New York Times </i>that immigration agents came knocking on her trailer door at 3 a.m. and arrested her husband. "Everyone is very scared, especially after they arrested people at their homes," Maritza told the reporter, tears welling in her eyes as she worried about how she would care for her four children while her husband languished in detention.<p>
"A lot of people are going back to Mexico," said Jazmin Gastelum, pointing out that stores serving Latinos have experienced a 40 percent drop in business. "And a lot who haven't moved are scared to go outside."<p>
The raids are having such an impact that even employers are worried--that they won't be able to find enough workers to keep their profits up.<p>
Smithfield is increasingly looking to recruit out-of-state workers to deal with high turnover rates. The company hopes workers from outside the area may not have heard about stressful and dangerous conditions at the slaughterhouse.<p>
"Thousands and thousands of workers from North Carolina have come through the plant, and they left, saying, 'No way,' because they were injured or didn't want to work in such an oppressive atmosphere," said Gene Bruskin, director of a United Food and Commercial Workers union organizing campaign at Tar Heel. "This plant burned up a large number of people, and the word got around about their bad experiences."<p>
Many employers hoped that Congress would pass immigration reform legislation. The corporate-friendly proposals, supported by both the Bush White House and a majority of congressional Democrats, would have maintained immigrant workers as second-class citizens, allowing companies to keep them on at low wages, working in poor conditions.<p>
But the legislation failed to pass earlier this year, and with Immigration and Customs Enforcement (ICE) raids terrifying immigrant workers, many employers face increasingly intense labor shortages.<p>
A recent string of ICE raids in Nassau County, N.Y., supposedly targeting gang members, prompted the <i>New York Times </i>to slam the agency for its "seriously botched 'cowboy' operation."<p>
"Armed squads bursting into homes in the dead of night with shotguns and automatic weapons, terrorizing families and taking away anyone who lacks identity papers, even if they have raided the wrong house," the <i>Times</i> editorialized. "It may sound like Baghdad, but it is the suburbs of New York City, the latest among hundreds of communities around the country where federal agents have been invading homes and workplaces in search of immigrants to deport."<p>
Immigrant rights activists need to continue their efforts to oppose ICE raids wherever and whenever they happen--and support efforts of immigrant workers to defend themselves and their families.<p>

