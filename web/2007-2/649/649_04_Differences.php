<font face="Times New Roman, Times, serif" size="5"><b>Real differences or imagined ones?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 19, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">ON WHAT the antiwar movement should or should not "set aside" to build wider alliances ("Why is the antiwar movement so weak?" October 12), you can read the demands and slogans of the October 27 Mobilization Committee, its flyers and, on the day of the event, the large "All Troops Out Now" banners we're making for the stages.<p>
Those demands should not and will not be "set aside," and if you are going to quote me otherwise, the courtesy of a phone call might have helped matters to clarify real differences, rather than imagined ones.<p>
Your accusation that I or any of us organizing this project is giving up this stand is simply mistaken. The only two elected officials confirmed as speakers so far, Alderman Joe Moore and Alderman Rick Munoz, take an "out now" position. Any other elected officials who speak on October 27 will either do the same, or will adapt to us, to one degree or another, not us to them.<p>
We have an excellent and diverse program shaping up, which will serve the antiwar struggle well. If you want to criticize it, fine, but criticize what it actually turns out to be, rather than gossip or speculation. <br>
<b>Carl Davidson</b>, Chicago<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><font face="Times New Roman, Times, serif" size="4"><b>Eric Ruder replies</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CARL DAVIDSON complains that my article wrongly accuses him of watering down the demands of the October 27 antiwar mobilization in Chicago.<p>
Don't take my word for it. The Eighth Day Center for Justice, a faith-based antiwar group, last week withdrew its endorsement of the Chicago protest because of the speaking invitation that Carl's group extended to Illinois' two Democratic senators, Richard Durbin and Barack Obama, as well as Chicago Mayor Richard Daley.<p>
Eighth Day's statement points out that Durbin voted October 1 for an additional $150 billion in war funding, and later for the Kyl-Lieberman amendment that sets the stage for war with Iran. Obama didn't show up to vote against either measure.<p>
Carl can make as many "All Troops Out Now" signs as he wants, but if he invites funders of the war to address an antiwar rally, he is compromising its message and failing to address one of the central political questions confronting the movement--that leading Democrats, whatever their rhetoric, support the war and must be challenged.<p>
Carl doesn't dispute the fact that the planning meetings he organized were invitation-only to keep out groups deemed "too left." So as far as courtesy is concerned, we think Carl should be more worried about not excluding those who have proven to be most dedicated to building the antiwar movement.<p>

