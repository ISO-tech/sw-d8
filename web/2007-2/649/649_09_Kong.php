<font face="Times New Roman, Times, serif" size="5"><b>A tale of two gamers</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Jim Ramey</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 19, 2007 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i><a href="http://www.billyvssteve.com/" target="_blank">The King of Kong: A Fistful of Quarters</a>,</b></i> a documentary by Seth Gordon.</font><p>

<font face="Times New Roman, Times, serif" size="3">FOR THOSE of us who wasted too much of our youth button mashing three feet in front of a television screen trying to get Mario through the tubes, there's a special place in our hearts for Donkey Kong.<p>
The first Mario game was also the first platforming video game, which places a premium on timing and hand-eye coordination. This made Donkey Kong incredibly difficult for the player, so much so that a game lasting 10 minutes is still considered a quarter well spent.<p>
Donkey Kong and most of the early video games couldn't be beaten, not because of their difficulty but because the programmers didn't bother with endings, so that if you actually reach the final level you're simply killed off.<p>
In Donkey Kong, where Mario is trying to save a woman from a rampaging gorilla, each successful level ends with a heart appearing above the woman's head and then breaking as the gorilla whisks her away to the next level.<p>
So it is a game that ends literally in heartbreak if you're good, or more likely in death. These matters of constant frustration and unfair competition are at the heart of the documentary <i>The King of Kong: A Fistful of Quarters.<p>
</i>The film is the study of two men in competition over the record high score in a single game of Donkey Kong. It plays out like a Christopher Guest mockumentary, except that the story is true and therefore funnier.<p>
The two men couldn't be more opposite. Billy Mitchell, the record holder for over two decades, is the picture of arrogance. Possessing a hairdo that looks expensive, though dreadful, and always wearing a patriotic neck tie, Mitchell turned his notoriety as the best video game player of the century into a chicken wing hot sauce business that allows him to pursue other records.<p>
"USA" is the three-letter initial Mitchell uses on arcade games to show that he holds the record on that machine. He says that he has "Latin friends and Canadian friends, and this is just to let them know who's number one."<p>
Steve Wiebe is Mitchell's counterpart. He's a science teacher whose life story is summarized in one day that should have been a moment of accomplishment. On the day he signed the papers on his house, he was laid off from his job as an engineer at Boeing.<p>
The third party in this story is a wannabe power ballad singer named Walter Day who created Twin Galaxies, a gatekeeper television program and now Web site of video game records.<p>
Without giving away the ending, the movie is <i>Rocky </i>for geeks. When Wiebe finally demands a head-to-head challenge, in Mitchell's home arcade no less, we already know what will happen as Leonard Cohen's "Everybody Knows" plays with brilliant effect when the rivals finally meet.<p>
Ultimately, it's inspiring to see how Steve Wiebe, a man whose name is constantly mispronounced throughout the film without him correcting anyone (pronounced weeBEE), is able to find his courage and take a stand against a corrupt system and an arrogant antagonist.<p>
For Wiebe, it wasn't getting laid off and having to "gut it out" for a year to get his teacher's license to help provide for his two kids with his partner. For him the breaking point was Donkey Kong.<p>
This film shows conclusively that you never can tell when struggle will break out but it is a blast when it does.<p>

