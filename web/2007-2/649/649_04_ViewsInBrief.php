He has been on death row for 10 years and just gave up his appeals. He seems ready to die, and who can blame him? He is 34 and has spent 10 years waiting for this day, with no chance of a commutation to a life sentence. He is guilty and has always admitted his guilt.<p>
I am extremely saddened by this. I did want to let you know that at least one execution will be taking place, since your story says that all death row prisoners may have a reprieve ("Death penalty derailed," October 12). <br>
