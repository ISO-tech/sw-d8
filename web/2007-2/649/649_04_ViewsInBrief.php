<p><a name="New"></a><font face="Times New Roman, Times, serif" size="4"><b>Neither new nor unique</b></font><p>

<i>THE NEW York Times</i> wants us to believe that the Bush administration's use of torture is an historical aberration, editorializing that torture is "abhorrent both to American law and values and international norms," and that former Attorney General Alberto Gonzales' authorizing of torture constitutes an "expansive endorsement of the harshest interrogation techniques ever used by the Central Intelligence Agency" ("The American way of torture," October 12).<p>
All this is self-serving rubbish. Declassified School of the America training manuals reveal that the very same torture techniques--use of extreme temperatures, sensory and sleep deprivation, sensory overload, humiliation, and "stress positions"--were all taught to prospective police and military personnel from Latin America.<p>
It is widely documented that systematic torture was used by U.S. forces both in the conquest of the Philippines (including "waterboarding") at the turn of the last century and in the invasion of Vietnam.<p>
According to historian Alfred McCoy, the CIA's notorious Phoenix program in Vietnam had "agents...operating 40 interrogation centers in South Vietnam that killed more than 20,000 suspects and tortured thousands more." And then there is the long history of police and prison torture in the United States.<p>
What makes this administration's approach a departure is less the practice of torture and more the brazenness of its support for it. What used to be denied is now almost proudly admitted--with the caveat that it isn't really torture.<p>
September 11 did not "cause" a turn toward torture any more than it "caused" the United States to invade Iraq. It merely provided a pretext for the United States to pursue more openly and aggressively its imperial aims, of which torture plays a component part.<p>
In our legitimate zeal to reveal the particular depravity of this administration, let us not overlook the fact that torture is an American ruling-class pastime.<br>
<b>Paul D'Amato</b>, Chicago<p>

