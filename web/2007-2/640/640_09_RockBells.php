<font face="Times New Roman, Times, serif" size="5"><b>Which way forward for hip-hop?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Leia Petty</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 17, 2007 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Rock the Bells Festival, visit <a href="http://www.rockthebells.net" target="_blank">www.rockthebells.net</a> for local show times.</font><p>

<font face="Times New Roman, Times, serif" size="3">WARNING: NOTHING should stop you from seeing this show--not torrential downpours, the long lines or the large number of frat boys. Fundraise if you have to.<p>
The 2007 tour of Rock the Bells brings together an unprecedented crew of hip-hop legends like Rakim and Public Enemy, the newer generation of independent rappers like Mos Def, The Roots, Nas and Talib Kweli, and gives voice to less mainstream artists such as Immortal Technique, Jedi Mind Tricks and Boot Camp Click--hosted by free-stylist Supernatural and the insane beat boxer, Rahzel.<p>
If this isn't enough, the tour is headlined by the Wu-Tang Clan and a reunited Rage Against the Machine.<p>
Again, nothing should stop you from seeing this show. This isn't the first year of Rock the Bells, but judging from the lineup, it is the most significant. The artists represent both the history and future of a genre that, judging from most of what is allowed on the radio, has been emptied, repackaged and marketed to an (increasingly middle-class and suburban) audience void of its historic social content.<p>
This tour showcases the artists that remain true to hip-hop's legacy of infusing progressive and radical ideas with danceable beats that find a way of lending rhythm even to the most awkward hip-hop head. And it couldn't have come at a more important time.<p>
After Don Imus was canned for his racist remarks about the Rutgers women's basketball team, the media turned the tables and blamed hip-hop music for racism and sexism in U.S. society.<p>
Since Rage Against the Machine's reunion, Fox News' <i>Hannity and Colmes</i> wasted no time launching their attack. In response to lead singer Zach De La Rocha's comment that Bush should be tried and killed for war crimes, Ann Coulter, an invited guest on the show, articulately responded that, "They're losers, their fans are losers, and there's a lot of violence coming from the left wing." Ouch.<p>
At two Rock the Bell stops in New York, artists responded with musically diverse and politically charged sets that kept fans bobbing their heads for over 10 hours. The anger at Bush and the war was palpable at the concert, with Public Enemy and Rage Against the Machine leading the charge.<p>
The repressive climate immediately following 9/11 seems to have cleared and given confidence to historically outspoken and political groups to take center stage again. Even Flavor Flav, after unabashedly promoting his terrible reality show <i>Flavor of Love,</i> called for the immediate withdrawal of U.S. troops from Iraq.<p>
Rage Against the Machine's set didn't disappoint. Favorites such as "Guerilla Radio" and "Take the Power Back" were performed as though the band had never broken up. Even Cindy Sheehan was impressed, commenting from the VIP section, "The crowd was filled with as much life as the band and I was amazed at all the energy...I was wondering how the peace and justice movement could harness that energy in a positive direction to really shutting down the machine."<p>
Immortal Technique, introduced as the "Che Guevara of hip-hop," performed "Bin Laden" with the crowd singing along: "Bin Laden didn't blow up the projects/It was you/Tell the truth." This wasn't the only song the crowd joined in on. Mos Def and Talib Kweli, bringing Black Star back to life, united for songs "Definition" and "Respiration." Public Enemy performed "Fight the Power" with intensity and conviction and, later in the set, brought Anthrax guitarist Scott Ian on stage for "Bring Tha Noize."<p>
The second stage showcased underground artists Mr. Lif, the Living Legends crew, and MF Doom. And if they represent the future of hip-hop, the main stage was a reminder that they are standing on the shoulders of giants.<p>

