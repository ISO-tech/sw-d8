<font face="Times New Roman, Times, serif" size="5"><b>Putting miners' lives at risk for profits</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 17, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">HOPE SEEMED to fade for the rescue of six trapped Utah coal miners as <i>Socialist Worker</i> went to press.<p>
The miners were trapped 1,500 feet down as they worked before dawn on August 6 in the Crandall Canyon Mine. Cameras and other equipment lowered into the mine had not found any evidence of the men since the collapse.<p>
But while mine co-owner Robert Murray--the head of Murray Energy, the nation's 12th-largest coal company--blamed the collapse on a "natural disaster," the reality is this latest American mining disaster has its roots in unsafe conditions and the drive for profits that are the hallmarks of the coal industry.<p>
Since the cave-in, Murray's statements to the press seemed to suggest his concern for workers' safety comes a distant second to his concern for profits.<p>
He insisted that the cave-in was caused by an earthquake, despite the fact that seismologists said this wasn't true. Murray has also repeatedly lashed out at any suggestion that poor safety conditions and mining practices may have been to blame, saying such suggestions are propaganda from the United Mine Workers of America union.<p>
Though Murray has angrily denied it, records show that the mine had a permit for "retreat mining," under which an almost-exhausted mine is squeezed for its last bits of coal when miners excavate the rock pillars that hold up the roof of the mine, and then pull them down, leading to what they hope will be a controlled collapse.<p>
"I wish you would take the word 'retreat mining' out of your vocabulary," Murray sneered at reporters last week. "Those were words invented by [former Mine Safety and Health Administration (MSHA) head] Davitt McAteer [and former federal mine safety official Tony] Oppegard--who are lackeys for the United Mine Workers--and officials at the United Mine Workers that would like to organize this coal mine."<p>
In addition to being viciously anti-union, Murray's mines have a long history of safety violations.<p>
Though experts say the Crandall Canyon Mine's safety record was within "industry standards," federal inspectors cited the mine for 30 violations so far in 2007--proving just how lax industry standards are overall. Since 2004, according to the <i>New York Times</i>, inspectors have issued 324 violations for the mine, resulting in fines of more than $130,000. Of those violations, 107 were considered "significant and substantial."<p>
Murray is heavily tied to the Republican Party as well, having given $100,000 last year alone from his political action committee to GOP congressional candidates. He has reportedly used his ties with prominent Republicans--particularly Senate Minority Leader Mitch McConnell, whose wife, Labor Secretary, oversees mine safety--to avoid having to address safety violations.<p>
But Murray is by no means an anomaly in the coal industry. Coal mining deaths soared to a 10-year high of 47 last year. The spike was the biggest percentage increase in 107 years, according to federal records.<p>
As Davitt McAteer put it, "The big push is to get the black stuff out of the ground. You neglect infrastructure, maintenance, education and (safety-law) compliance. It begins to catch up with you."<p>

