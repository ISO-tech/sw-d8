<a name="Teamsters"></a><font face="Times New Roman, Times, serif" size="4"><b>Did the Teamsters win in Oakland?</b></font><p>

<font face="Times New Roman, Times, serif" size="3">TODD CHRETIEN'S article paints an accurate picture of the struggle that took place between Waste Management (WM) and the Teamsters ("Solidarity defeats the garbage bosses," August 3). But I think "victory" for the Teamsters and a "defeat" of WM is the wrong way to describe the outcome, because the new contract doesn't seem to be an advance over the old one.<p>
True, the Teamsters did get raises, but as Bob Kuykenball (of the Teamsters) and Don Crosatto (of the Machinists) explained in <i>SW</i>'s coverage of the lockout, this dispute wasn't really about wages. The disciplinary procedures were the central issue. The right to honor other unions' picket lines was another big part of the fight.<p>
I also gathered from workers on the picket line that the Teamsters' right to strike over grievances was a major sticking point. In fact, two long-time Machinists explained to me that the main reason the Teamsters have been able to win meaningful pay raises--whereas the Machinists' raises have just barely kept up with cost-of-living increases--is that the Teamsters had the right to strike during the contract and the Machinists did not.<p>
So on balance, the Teamsters defended the right to honor picket lines, which is one of the most powerful weapons of solidarity of the Bay Area labor movement. But they lost ground on the disciplinary procedures, and they lost the right to strike over grievances.<p>
I don't think we should call this a victory. Too many "victories" like this, and the Teamsters at WM could be seriously weakened.<p>
Does that mean that a "no" vote on the contract was in order? Not necessarily, because retreating or accepting a draw is not always the wrong thing to do. It depends on how strong you are in relation to your opponent. And make no mistake about it, the Teamsters put up a great fight considering the scale of the scabbing operation that WM ran.<p>
In any case, though, we should be able to tell the difference between winning and not losing as badly as was possible. The Teamsters, machinists and longshore workers put up strong picket lines and demonstrated exemplary solidarity.<p>
But at a time when there is no shortage of labor leaders in other unions willing to substitute blustery, triumphalist rhetoric for actual organizing and struggle, and when the resulting massive concessions are often sold to the rank and file as decent or necessary compromises, the labor movement only stands to gain from having a very sober and sharp assessment of the relation of forces and the outcome of struggles.<br>
<b>Sid Patel</b>, San Francisco<p>
