<p><a name="Afghans"></a><font face="Times New Roman, Times, serif" size="4"><b>"Liberating" Afghans from life</b></font><p>

<font face="Times New Roman, Times, serif" size="3">A RECENT Associated Press report on U.S. casualties in Afghan war indicates that the deaths stood at 333 as of June 7. The civilian casualties in Afghanistan from January of this year through June were reported to be between 320 and 380. May, a particularly deadly month for Afghan civilians, saw an equal number of deaths from U.S. or NATO forces and the Taliban.<p>
It was reported that those deaths "include about 135 killed by U.S. or NATO action, a figure that could also undermine support in western countries, especially in Europe, for the far away deployment."<p>
The report goes on to say that "about 135 civilians have also been killed by Taliban suicide bombs and attacks" in May. U.S. Special Forces recent operations in Afghanistan have also killed 90 civilians according to another recent Associated Press report. The combined civilian deaths from ground attacks and aerial bombings have resulted in large street demonstrations and increased skepticism among Afghans about U.S. motivations.<p>
In response to this year's stepped-up bombings and apparent disregard for civilian casualties, the Afghan upper house of Parliament has called on U.S. and NATO-led forces to stop taking offensive actions against the Taliban. They also asked the Afghan government to open up dialogue with the Taliban provided the Taliban accept the country's new constitution. The upper house also asked for a timetable for the withdrawal of foreign troops.<p>
The apparent disregard for women, children and the elderly in attacks on civilians from both sides indicates a lack of legitimacy--a situation where the "war on terror" has become a "war of terror."<p>
Some of the earlier promises of the U.S. in Afghanistan--to build schools, hospitals and roads--have been only partially fulfilled, and many schools have been closed because of the violence. Many of the ideas such as building schools and infrastructure are sound, but were minimally fulfilled, and girls' schools only seem secure in the capital city of Kabul.<p>
Some believe the primary reason for the war in Afghanistan was not to free the nation from the Taliban but to clear up a thoroughfare for Caspian Sea oil. The route for Caspian Sea oil was/is to be trucked from the Caspian Sea through Turkmenistan, Afghanistan and Pakistan to the port at Karachi, and from there down the Arabian Sea and to U.S. ports. Time will tell if this pans out and is the actual motive.<br>
<b>Brian McAfee</b>, Muskegon Heights, Mich.<p>

