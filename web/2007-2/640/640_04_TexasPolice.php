<font face="Times New Roman, Times, serif" size="5"><b>The police beating that caused a death in Texas</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 17, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">ON JULY 22, a little after 2 a.m., the Pasadena, Texas, police dealt Pedro Gonzales Jr. a fatal blow to his ribs. He would later be pronounced dead in his jail cell at around 7:30 a.m.<p>
Pasadena is a city just east of Houston usually known for its chemical plants and oil refineries. A cursory search will tell you that Pasadena is one of the fastest-growing cities in Texas, with a population of 145,000. It is also touted by its official Web site as "one of the safest places to live." The site describes its police force as "innovative."<p>
I am sure that Mrs. Gonzales is happy to know that the police force that killed her husband used "innovative" methods to do so.<p>
Early Saturday at 1 a.m., Mr. Gonzales was released from jail with 64 cents in his pocket. An hour later, he was spotted by officers Jason Buckaloo and Christopher Jones, who said in interviews that Mr. Gonzales appeared to be intoxicated while sitting in the bed of a truck about a mile away from the Pasadena Jail.<p>
They proceeded to arrest him on the grounds of public intoxication. In none of the information released to the press do the police claim to have administered a breathalyzer test to prove their assumptions.<p>
Rick Dovalina, spokesperson for the League of United Latin American Citizens Council (LULAC), said, "There is no way he would have been able to buy alcohol because he only had 64 cents in his pocket when he left the jail." It was 1 a.m., which means stores in the area had stopped selling alcohol, and he could not have gone to a bar as he was in a "dry" part of town.<p>
The original story the police put out was that Mr. Gonzales tripped and fell as he was escorted to the police car. The officers claimed that he was "combative" during the arrest, as reported on Houston's Channel 2 News. What was left out of the original story was that there was a witness to Gonzales's "combativeness."<p>
At 2:09 a.m., Evelyn Moreno called 911 to report that she had seen the officers beating a motionless Gonzales for two minutes. As reported in the <i>Houston Chronicle</i>, "The 20-year-old woman was driving home when she noticed the officers hitting Gonzales."<p>
On the recording of the call it is difficult to hear Moreno, as she called from a pay phone, since she originally wished to remain anonymous. The 911 operator indicated that she understood Moreno was reporting an incident of police brutality. Moreno was then given a non-emergency number to call, though she did not. She did not think she would be taken seriously. Would the police come to police themselves?<p>
The police incident report states: "We administered several knee strikes and elbow strikes to Pete's back and thighs, and ordered him to stop resisting. Pete grabbed hold of my legs and pants, and attempted to knock me over, and spun around and kicked me in the leg. I continued to administer knee strikes to Pete and ordered him to stop resisting and place his hands behind his back."<p>
However, in her 911 call, Moreno reported that the officers were beating a motionless Gonzales. A preliminary autopsy report states that Mr. Gonzales died because a bone fragment from a fractured rib punctured his lung.<p>
Officer Buckaloo had previously been indicted on charges of using excessive force on a 15-year-old South Houston High School student in 2001. A jury found him not guilty in a 2002 trial of official oppression.<p>
The murder of Pedro Gonzales Jr. is one of many examples of the unbridled police brutality occurring in the Texas criminal injustice system. It is, of course, a reflection of institutionalized racism and the politics of brutality in this country. It will take a strong movement to put an end to these atrocities.<br>
<b>Karen Burke</b>, Austin, Texas<p>

