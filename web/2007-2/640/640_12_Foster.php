<font face="Times New Roman, Times, serif" size="5"><b>The race to save an innocent man</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 17, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>BRYAN McCANN reports on how justice was denied again for Texas death row prisoner Kenneth Foster.</b><p>

ALL EYES are on Texas Gov. Rick Perry after the state's highest court dismissed a final legal appeal from death row prisoner Kenneth Foster Jr. without even bothering to write an opinion.<p>
Kenneth faces an execution date of August 30 for the "crime" of driving a car. In August 1996, Mauriceo Brown got out of that car, and committed a robbery, during which Michael LaHood Jr. was shot and killed. But under Texas' "Law of Parties," Kenneth was tried and sentenced as if he pulled the trigger.<p>
His attorneys presented additional evidence to the Texas Court of Criminal Appeals that Kenneth had no idea Brown planned to harm LaHood. But in a 5-3 decision, with one judge abstaining, the court denied this latest appeal.<p>

