<font face="Times New Roman, Times, serif" size="3">Now Kenneth's supporters want to put pressure on Perry, who can grant clemency. Five of the seven members of the Texas Board of Pardons and Paroles must recommend clemency for Perry to even consider granting it. But Perry appointed the board members, and while it officially operates independently, the board is known to respond to pressure from the governor's mansion.<p>
Kenneth's case has drawn national and international attention because of the slender excuse that the state of Texas has to send him to the execution chamber. No one, not even prosecutors, claims that Kenneth fired the weapon that killed LaHood--in fact, he was sitting in the car 80 feet away when the crime took place.<p>
But the state claims he should be put to death on the basis that he should have "anticipated" a crime would take place. "We've all heard of hindsight being 20-20," Kenneth said in an interview with <i>Socialist Worker</i> published last month. "but Texas has created an inconceivable concept, and that's foresight being 20-20--i.e., you better be psychic."<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">THE TEXAS Court of Criminal Appeal's cruel ruling is a stinging setback for the Save Kenneth Foster Campaign, a coalition formed earlier this year after an execution date was set, and led by Kenneth and his family. But activists have known that they were working against the odds--and they aren't giving up.<p>
With two weeks to go before the scheduled execution date, the campaign has a number of events planned to raise public awareness about the case and apply pressure to Perry and the Board of Pardons and Paroles.<p>
On August 14, the Campaign to End the Death Penalty hosted a moving public forum titled "Family Members Speak Out!" which featured members of Kenneth's family, as well as Sandra Reed, the mother of innocent Texas death row inmate Rodney Reed; Delia Perez Meyer, the sister of innocent death row prisoner Luis Castro Perez; and Jeannine Scott, whose husband, Michael Scott, was wrongly imprisoned for the 1991 Yogurt Shop Murders in Austin.<p>
On August 15, the Save Kenneth Foster Campaign was set to hold a press conference at the State Capitol. The Fosters, as well as Kenneth's attorney, Keith Hampton, will comment on the Court of Criminal Appeals decision and what's next for the movement.<p>
On August 18, the coalition is holding a "Night of Solidarity with Kenneth Foster" in San Antonio, featuring local hip-hop artists, as well as activist and writer Mario Africa, who spoke at an Austin rally for Kenneth in July. Activists are also working hard to build an August 21 emergency rally back in Austin.<p>
"It's my belief that if this does not become a political issue, then I have no chance," Kenneth wrote shortly after receiving an execution date.<p>
He's right. Rick Perry won't spare Kenneth out of the kindness of his heart. Having overseen 159 executions since he took office in 2001, Perry has outdone even his predecessor George W. Bush. This summer, Texas will carry out its 400th execution.<p>
It will take nothing short of a visible, militant and vibrant movement to apply the political pressure necessary to save Kenneth's life.<p>

