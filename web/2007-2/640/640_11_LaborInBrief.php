<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">August 17, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Police attack labor rights protest</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Brian Chidester</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NORTH PROVIDENCE, R.I.--Police attacked a peaceful demonstration for labor rights August 11, pepper-spraying several protesters, arresting two and seriously injuring one of those arrested.<p>
The protest of 40 people was called by the Providence chapter of the Industrial Workers of the World and included members of Students for a Democratic Society from Providence and Boston. The demonstrators were targeting Jacky's Galaxie restaurant, a pan-Asian restaurant that is supplied by HWH/Dragon Land Trading, a sweatshop operation based in New York.<p>
Protesters were marching up Mineral Spring Avenue when police ordered them onto the side of the road or the sidewalk. Though the group complied, the police attacked some of the marchers without warning.<p>
One, 22-year-old Alex Svoboda "was pinned down by the police during her arrest and suffered a broken and dislocated leg, and ruptured blood vessel in the knee. These injuries will require at least two surgeries and extensive rehab, and even then, may not completely repair the damage," wrote SDS activist Michael da Cruz in an e-mail.<p>
As Green Party member Steve Lamarre described the scene, "Police say she was resisting, but aside from wanting to avoid broken glass, the only other 'resistance' I could discern was they were putting extreme pressure on her left leg, which snapped at the knee after two officers put enough of their weight on it."<p>
North Providence police used an exaggerated estimate of 100 protesters to claim that they were outnumbered. They charged Svoboda with three counts of assaulting a police officer, and arrested her roommate Jason Friedmutter, charging him with obstruction of a police officer.<p>
Activists plan to continue pickets at the North Providence restaurant and other locations. In addition, they are organizing a protest march in response to the violence of the police.<p>
