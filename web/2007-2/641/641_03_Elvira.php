<font face="Times New Roman, Times, serif" size="5"><b>Feds take revenge against Elvira</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Orlando Sep&uacute;ldeva and Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 24, 2007 | Pages 1 and 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THE HEAVY-HANDED arrest and immediate deportation of immigrant rights activist Elvira Arellano on August 19 sparked anger and activism across the U.S.<p>
The undocumented mother of a U.S. citizen, Arellano last year became the face of the estimated 12 to 14 million immigrants without papers in this country. Arrested in an Immigration and Customs Enforcement (ICE) raid at O'Hare Airport in Chicago, Arellano publicly defied an order of deportation and sought sanctuary in a Chicago church known for its civil rights activism.<p>
For the past year, she and her 8-year-old son, Sa&uacute;l, have lived in a small apartment above Adalberto United Methodist Church. Thousands of people from around the country and internationally visited her there, and millions more heard about her case through the media.<p>
Her stand at Adalberto Church helped spur the creation of new sanctuary movement, in which religious organizations throughout the U.S. have pledged to give material support to immigrants facing detention and deportation.<p>
In fact, Arellano's arrest took place after she left one of the congregations that is central to the sanctuary movement, Our Lady Queen of Angels Catholic Church, in the heart of the Mexican section of Los Angeles. Arellano had put herself at risk of arrest by traveling to Los Angeles to launch a national tour to try to revive legislation stalled in Congress that would allow the undocumented a path to legalization.<p>
For ICE--whose officials last year vowed to "take action at the time and place of our choosing"--it was an opportunity for revenge.<p>
According to witnesses, unmarked cars cut off and surrounded the SUV that Arellano was traveling in. ICE agents leapt out of the cars and screamed at her to surrender.<p>
First, Arellano paused to console her son. Then, she was handcuffed and taken away. Within hours, she had been processed at an ICE detention center, driven 100 miles to the Mexican border and deported to Tijuana.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">IN HER hometown of Chicago, immigrant rights activists turned out that same day in the pouring rain to picket the ICE office. About 80 supporters met outside the offices the following morning for an improvised press conference on Arellano's arrest, before numerous media outlets.<p>
The press conference started when Flor Cris&oacute;stomo, from Arellano's organization La Familia Latina Unida--relying on her bullhorn rather than the cameras, microphones and podium set up by the media--said, "They say that this is the land of freedom, but many abuses to our civil rights are perpetrated...They want to shut our strongest voice, but they won't be able to, because we all are Elvira's voice. All and each one of us is Elvira."<p>
Cris&oacute;stomo herself was victim of a raid, days before the massive rally of May Day 2006, at IFCO Systems in Chicago. Since then, with the help of Elvira, she has become another tireless anti-deportation activist.<p>
Tim Bell of the Chicago Workers Collaborative, pointing out that deportations were a tragedy that affected not only Mexicans, introduced Tony Wasilewski, whose wife was deported a couple of moths ago. Wasilewski spoke in support of Arellano, saying: "Elvira...los polacos estamos contigo"--Elvira, the Poles are with you.<p>
Between expressions of solidarity of many groups that came to the rally, Emma Lozano, leader of Centros Sin Fronteras, called in from Los Angeles and left a message from Elvira. "As you know," she said, "Elvira has already been deported to Mexico, but her spirit is strong, and she tells us that now is the moment to keep the pressure on for a moratorium on all deportations."<p>
That struggle is urgent. "In recent months, arrests of fugitives such as Arellano have been occurring at a pace of some 675 per week," <i>Business Week </i>reported. "Federal authorities have ramped up workplace raids and increased other types of immigration enforcement as part of a hardened approach to illegal immigration after a bipartisan bill that would have offered legalization failed in the Senate earlier this month."<p>
New regulations from the Department of Homeland Security require that employers take action against workers when their Social Security numbers don't match those in government files, setting the stage for a vast increase in arrests and deportations.<p>
Elvira Arellano's courageous stand will be an inspiration for those facing similar fights. As Joshua Hoyt, executive director of the Illinois Coalition of Immigrant and Refugee Rights, said, Arellano "taught us not to fear, fighting until the last consequences and giving a human face to the tragedy of millions. We have to thank Elvira for her courage."<p>

