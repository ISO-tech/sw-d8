<font face="Times New Roman, Times, serif" size="5"><b>America's infrastructure crisis</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 24, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">IT MAY have been clear to some of us, but with the recent collapse of the I-35W bridge in Minneapolis, the sorry state of America's infrastructure is being blown open for all of the U.S. to see.<p>
From the failed levees of New Orleans (which wreaked utter and complete havoc) to a blown pipe in Manhattan (again, havoc, although thankfully no deaths) to the seemingly one-in-a-billion failure of a well-trafficked bridge (miraculously, fewer deaths than originally expected), the U.S. government may finally be starting to get it into their heads that the pipes, sewer lines and structures that help make our country run are in need of some money.<p>
Special inspections and reports regarding the structural stability of various buildings have been occurring for years. More than 70,000 bridges across the country are currently rated as "structurally deficient" by the official government inspection standards.<p>
The American Society of Civil Engineers (ASCE) has been doing independent reports on infrastructure for years, and no aspect of U.S. infrastructure, in the minds of engineers, rates above a "C." Yet while so many of the results of various studies and surveys are poor, hardly anything is ever done to fix something until a serious failure occurs.<p>
Why is this? And why is it that, even though inspections occur, they often aren't on as regular a basis as they should be, or don't utilize the monitoring equipment available that would give engineers a much better sense of the state of a structure?<p>
The sad fact is that it costs too much money. Most of the new infrastructure construction or rehabilitation that occurs is paid for in the form of tax hikes. Minneapolis' great idea for finding the money to fix their ailing bridge? Crank up gas taxes--that already expensive commodity that your average working person is forced to buy simply to get to work.<p>
The money is out there to investigate and subsequently fix all that's wrong with our transportation, sewer and water systems. The ASCE report estimates that it would cost approximately $1.6 trillion over a five-year period, or about $320 billion a year (and creating as many as five million jobs in the process), to rehabilitate all of our ailing infrastructure. The Department of Defense budget for 2006? About $419.3 billion a year.<p>
The even sadder fact? New construction isn't always that safe. In an age when time is money, contractors often cut every corner possible to get buildings up in record time, working laborers to the bone and forcing <i>them</i> to cut corners to "get 'er done," while not paying attention to the details that can make a big difference in the long run; installing substandard products that cost less; and circumventing building departments and engineers to prevent being slowed down by inspections and waits for permits.<p>
The intent here isn't to make you run out of the building you're sitting in while reading this. <i>Most</i> engineers and <i>most</i> city officials try as much as possible to make sure that buildings and bridges are, in their final state, perfectly safe for use. Unfortunately, they can only do so much.<p>
It doesn't have to be this way. In a sane society, where the needs and safety of people really are at the forefront of every decision, engineers, construction workers and all those involved in the building trades would be able to do their jobs and do them well, without the pressure to work faster, harder and make it as cheap as possible.<p>
That society, a socialist society, is one that I someday hope to see, and one that through hell or high water (or collapsing bridges), I'll be out there fighting for.<br>
<b>Andrea Hektor, </b>Portland, Ore.<p>

