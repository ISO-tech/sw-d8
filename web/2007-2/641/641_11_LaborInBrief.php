<p><a name="Smithfield"></a><font face="Times New Roman, Times, serif" size="4"><b>Smithfield Packing</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WILLIAMSBURG, Va.--Workers at Smithfield Packing Company in Tar Heel, N.C., and supporters from several cities will be taking their message to the company shareholders' meeting here August 29: We have the right to unionize!<p>
The anti-union tactics at Smithfield--the world's largest pork-processing plant--are as vicious as their plant is dangerous. On August 6, management terminated Jose Ozorio Figueroa, claiming it was because he was four minutes late to his shift.<p>
But Figueroa, who has worked at Smithfield since 2004, and other pro-union workers know it's because of his activism for rights on the job. He was among a group of workers who took part in a walkout in November 2006 to protest the company's firing of workers whose Social Security numbers did not match federal records.<p>
Activists are working to build solidarity--inside the plant and around the country. Workers began circulating a petition five months ago inside the plant calling for clean drinking water to be made available at work, and walked out of the plant on August 7 after they had waited three hours for clean drinking water the day before in the unbearable 103-degree summer heat.<p>
According to the Justice at Smithfield campaign, 40 percent of the livestock workers walked out and held a sit-in on the grass outside the livestock area.<p>
"Basically, the workers decided that we are not going to work until we get this resolved," Keith Ludlum, one of the workers who walked out, told the <i>Fayetteville Observer</i>. "I think it is ridiculous to have to go to this measure in order to have clean drinking water for everyone."<p>
Meanwhile, supporters are taking this fight to the road. In Boston, supporters helped pass a resolution on August 1 that asks the city to suspend any purchases of products made at the Tar Heel plant "until the company ends all form of abuse, intimidation and violence against its workers."<p>
Activists from several cities will board buses to rally at the Smithfield's shareholders' meeting to present petitions signed by hundreds of supporters.<p>
