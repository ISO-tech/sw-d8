<a name="Home"></a><font face="Times New Roman, Times, serif" size="4"><b>Long Island, N.Y., home health aides</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Peter Lamphere</b></font><p>

<font face="Times New Roman, Times, serif" size="3">HEMPSTEAD, N.Y.--Banging pots and pans, shaking maracas and chanting, home health aides here led an inspiring picket of Premier Home Health Care in an effort to win their first union contract.<p>
The spirited five-day strike, which began August 13, ended without an agreement, but the yearlong effort to organize the workers into SEIU Local 1115/SEIU United Health Care Workers East continues.<p>
Overwhelmingly, those walking the picket line cited low wages and the need for health care and sick days as a motivating factor of the strike: most only make between $7 and $8 an hour.<p>
"Whatever it takes, we have to do it because we care for these people, and nobody cares for us," said Jean Douglas, one of the members of the union organizing committee and a 20-year veteran of the home health care industry.<p>
Four days into the strike, management finally responded to pressure to come to the bargaining table with a slap in the face: The company offered nothing for the first year, and a raise in the second only if Premier makes a profit. Another member of the union committee, Regina, summed up the sentiments of the workers about the offer: "It was disrespectful."<p>
Workers refused the offer, but on August 17 voted to return to work. The membership had only planned for a five-day job action, but plans to go on another strike should it be necessary.<p>
"I was ready for [organizing a union] because I had nothing," said Jean, "I still have nothing, because we don't have a contract yet, but at least I have hope. You've got to fight for what you want."<p>
The effort in Long Island is part of a long-term campaign by SEIU to organize the home health care industry. New York City Premier workers were organized in 2002 because of a strike threat, and tens of thousands of aides struck in New York City in 2004 to successfully win wage gains.<p>
<i>Call Premier President Arthur Schwabe at 914-428-7722 and tell him that his workers deserve better</i>.<p>
