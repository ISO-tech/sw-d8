<a name="Edimar"></a><font face="Times New Roman, Times, serif" size="4"><b>The death of Edimar de Araujo</b></font><p>

<font face="Times New Roman, Times, serif" size="3">EDIMAR ALVES de Araujo, a 34-year-old Brazilian immigrant living in Milford, Mass., is the latest casualty in the drive to criminalize immigrants in the United States.<p>
He was stopped by Woonsocket, R.I., police for a traffic violation on August 7. Less than four hours later, he was pronounced dead at Rhode Island Hospital in Providence.<p>
When the Woonsocket police found out that de Araujo had an outstanding deportation order, they detained him at the police station and called Immigration and Customs Enforcement (ICE) to take custody of him. They did this despite the fact that there's no legal agreement that allows local police in Rhode Island to work with ICE.<p>
The Woonsocket police ignored pleas by his sister Irene that he had seizures and needed medication for them. By the time de Araujo reached the ICE office in Providence, he was clearly in the midst of a seizure. By the time ICE agents got him to the hospital--just a few blocks from the ICE office--he was dead.<p>
De Araujo is the 62nd person to die in ICE custody since 2004. There appears to be no real consequence to ICE for this type of negligence or malice, and there is no compensation to the families of the victims. In other words, ICE can kill with impunity--and get the help of the local police as well!<p>
Coincidentally, it was Woonsocket's State Senator, Marc Cote--a Democrat--who was the driving force behind a spate of anti-immigrant legislation in the Rhode Island General Assembly this past spring. Fortunately, none of it passed.<p>
However, the problem goes beyond legislation: when undocumented immigrants are considered "criminals" by those in power, it gives cops the green light to dehumanize them and violate their human rights. This is why we need to proclaim: No human being is illegal!<br>
<b>Brian Chidester</b>, Warren, R.I.<p>
