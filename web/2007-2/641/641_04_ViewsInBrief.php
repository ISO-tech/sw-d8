<p><a name="Potter"></a><font face="Times New Roman, Times, serif" size="4"><b>Another chapter to the Potter story</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I READ the article "Harry Potter: The final conflict" (August 3). While the article was good, it merely skims the surface of the socialist message in <i>Harry Potter</i>.<p>
Rowling has said repeatedly that her heroine was self-taught socialist Jessica Mitford, and several character names from the book were taken from Mitford's autobiography, <i>A Fine Old Conflict</i>. The conflict in the book could easily be seen as an analogy of Mitford's own life growing up in a British aristocratic family, where her sisters were supporters of fascism and Hitler, and Jessica became the red sheep of the family, running off to fight in the Spanish Civil War and eventually emigrating to the U.S. and joining the Communist Party.<p>
I hope that <i>SW</i> will consider a follow-up story discussing how Rowling has repackaged socialist ideas in an attractive and marketable format.<br>
<b>John Rose</b>, from the Internet<p>
