<font face="Times New Roman, Times, serif" size="3">As IVAW executive director Kelly Dougherty said, "The convention went extremely well, with a lot of new members participating, including some who even joined during the conference. We built more bonds between ourselves and are expanding nationally in more parts of the country, with over 22 chapters currently."<p>
Workshops also reflected a sense of the need to take up larger political issues related to the wars in Iraq and Afghanistan. Dr. Dalia Wasfi, for example, spoke on the apartheid character of Israel's occupation of Palestine. Other workshops took up the nature of imperialism and strategies for counter-recruitment.<p>
When the IVAW discovered that military recruiters were attending an African American job fair at the St. Louis Convention Center a few blocks away, members organized an impromptu counter-recruitment event.<p>
Inside the auditorium, to counter the Army's new recruitment ploy--a multiplayer 3-D video game dubbed "America's Army"--IVAW members fell in formation and chanted, "Iraq veterans, what did we learn? War is not a game!" The action brought cheers from fair attendees, and one veteran asked how to get involved.<p>
The culmination of the national meeting was a march to the St. Louis Gateway Arch where hundreds gathered to demand "Troops out now!" Phil Aliff, a member of the active-duty Army and president of the IVAW Fort Drum chapter, captured the dynamism of the new GI movement when he addressed the crowd.<p>
"We're IVAW, and we're speaking for all the prisoners of conscious who are being held by the military in an illegal occupation of Iraq," Aliff said. "For them, and for the Iraq people, we say, 'Bring us home now.'"<p>

