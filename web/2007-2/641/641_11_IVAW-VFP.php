<font face="Times New Roman, Times, serif" size="5"><b>Antiwar veterans look ahead</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 24, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>IVAW member MARTIN SMITH reports from the VFP/IVAW convention held in St. Louis.</b><p>

ST. LOUIS--More than 400 veterans and antiwar activists, including at least 90 members of Iraq Veterans Against the War (IVAW), took part in this year's joint Veterans For Peace/IVAW National Convention from August 15-19.<p>
The event brought together the largest gathering of IVAW members to date for its second national convention. In addition to electing its national board, IVAW organized its own workshops at this year's convention.<p>
Discussions centered on implementing a national strategy based on chapter building, counter-recruitment activity, more bus tours to active-duty bases and organizing a send-off for IVAW members at Fort Drum in upstate New York who were recently activated for deployment.<p>
Tod Ensign of Citizen Soldier and several active-duty members from Fort Drum addressed GI resistance and its connection to the newly emerging GI coffeehouse movement. The participants noted that the success of IVAW in organizing around Fort Drum was due in large part to the efforts of activists from the Different Drummer Caf&eacute;, a GI coffeehouse in Watertown, N.Y., a few miles from the base.<p>
Other speakers included David Cortright, author of <i>Soldiers in Revolt</i>; Camilo Mej&iacute;a, the first active duty soldier to refuse deployment to Iraq; and members of Military Families Speak Out.<p>

