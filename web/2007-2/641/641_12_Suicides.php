<font face="Times New Roman, Times, serif" size="5"><b>Suicides of U.S. soldiers hit new high</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alison McKenna</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 24, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">THE SUICIDE rate among Army soldiers is the highest since 1980, the first year that the Army started counting, according to a report released August 16.<p>
According to the 2006 Army Suicide Event Report, there were 99 suicides last year, with nearly half of the soldiers under 25 years of age. Officials reported 948 suicide attempts, though that number is likely underreported. The study also found "a significant link between suicide attempts and the number of days deployed in Iraq, Afghanistan or nearby countries where troops participate in the war effort."<p>
So while initial reports for the first half of 2007 indicate the number of suicides could decline across the service, they could increase among troops currently serving in Iraq and Afghanistan.<p>
With the Army stretched thin, the Pentagon has extended normal tours of duty to 15 months from 12, and has sent many troops on repeated tours. Broader questions regarding the increase in suicides relate to the morale of soldiers who may not support these unpopular wars.<p>
Sen. Patty Murray (D-Wash.), a member of the Senate Veterans Affairs Committee, cites the stresses of longer and repeated tours of duty and her suspicion that many in the military don't understand how to deal with post-traumatic stress disorder (PTSD). "I think there is just an inner denial among some that PTSD is 'you're just not tough enough,'" she said.<p>
Military officials report that about 35 percent of soldiers are seeking mental health treatment a year after returning home. Failed personal relationships, legal and financial problems, and work stress were all factors motivating the suicides.<p>
Researchers studied the home life of 168 soldiers diagnosed with psychological symptoms upon their return home from deployment. The PTSD finding has been observed in other studies, but the link between returning veterans' depression and family trouble is new, experts said.<p>
Deborah Gibbs, a senior analyst at RTI International, concluded that the study "underscores the fact that deployments are tough on all family members--they're tough on the parent who cares for the children...and for the entire family that has to deal with all the soldier's experiences once they return."<p>
Family troubles included physical or mental abuse, with 35 percent of the soldiers studied saying they felt that their partner was now fearful of them.<p>
"We do know that increased exposure to wartime trauma increases the risk for all kinds of mental-health issues," said Steven Sayers, lead researcher and clinical psychologist at the Philadelphia VA Medical Center.<p>
PTSD is now being called "a signature injury of the conflict in Iraq." That this trauma should be striking hard among veterans of the Iraq war should be of no surprise--it is the predictable consequence of an occupation for the U.S. empire.<p>

