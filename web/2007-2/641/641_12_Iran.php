<font face="Times New Roman, Times, serif" size="5"><b>Inventing reasons for targeting Iran</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 24, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>ELIZABETH SCHULTE looks at the latest escalation in the Bush administration's war threats against Iran.</b><p>

WITH RUMORS flying that administration hawks are pressing for a military attack on Iran before George Bush leaves office, officials announced last week that the U.S. government was set to name the Iranian government's Revolutionary Guard (IRGC) a "specially designated global terrorist."<p>
This will be another first for the Bush administration--naming a part of the armed forces of a sovereign nation a "terrorist organization."<p>
According to press reports, if Condoleezza Rice's State Department has its way, Iran will be listed along with 42 other organizations it considers "foreign terrorists," including al-Qaeda, Hezbollah and Hamas.<p>
The Bush administration has always accused Iran of sponsoring terrorism, but specifically naming the Revolutionary Guard would be a major escalation.<p>
The "global terrorist" designation--the result of Executive Order 13224, which Bush signed two days after September 11--brings with it the possibility that the U.S. can trigger economic penalties on people or companies that do business with the Guard. It would be illegal for anyone subject to U.S. law to knowingly provide material support to IRGC.<p>
"Should the terror label on the IRGC be in place soon, U.S. customs and Homeland Security officials could, theoretically, arrest members of [Iranian President Mahmoud] Ahmadinejad's delegation due to travel to the UN headquarters in New York next month because of suspected ties to the IRGC," wrote Kaveh Afrasiabi in the <i>Asia Times.</i> "Even Ahmadinejad, with his past as a commander of the Basij Corps, a paramilitary arm of the IRGC, risks arrest."<p>
But more than this, labeling the 125,000-strong Revolutionary Guard a "terrorist" organization represents a step-up in U.S. threats against Iranian sovereignty and puts the idea of "regime change" on the table once again.<p>
The U.S. says that it is targeting Iran because of its alleged manufacturing of nuclear weapons and aid to insurgent groups that oppose the U.S. occupation of Iraq. "We are confronting Iranian behavior across a variety of different fronts, on a number of different, quote unquote, battlefields, if you will," State Department spokesman Sean McCormack told reporters.<p>
 At the same time, the U.S. is trying to push the UN Security Council to enact further sanctions against Iraq, to go into effect in the fall. On several occasions, including the invasion of Iraq, the U.S. has used UN sanctions--or a country's alleged violation of them--as a prelude to war.<p>
Hoping to embarrass Iranian officials and threaten anyone who talks to them, the terror designation was reported the same week that Ahmadinejad was on a high-profile trip to Afghanistan.<p>
The Bush administration accuses Iran of arming the Taliban in Afghanistan--even though the Revolutionary Guards, including the elite Quds division, worked with U.S. forces to help anti-Taliban fighters topple them from government in 2001.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">FOR IRAN, this has been a summer of endless threats from the U.S.<p>
In late July, Bush gave a "shoot-to-kill" order against supposed Iranian operatives accused of aiding the anti-occupation insurgents in Iraq, even though there is no proof that these operatives exist.<p>
According to two U.S. officials involved in Iran policy, Vice President Dick Cheney proposed launching airstrikes earlier this summer at suspected training camps in Iraq run by the Quds force.<p>
And in June, Democratic Party hawk Joe Lieberman called for cross-border raids into Iran--while Israeli Trade Minister and former Defense Minister Shaul Mofaz proposed bombing Iran's nuclear plants.<p>
Meanwhile, in early August, Bush threatened Iraqi Prime Minister Nouri al-Maliki for daring to visit Tehran. "My message to him is, when we catch you playing a non-constructive role, there will be a price to pay," said Bush.<p>
The media has added its voice to the anti-Iran clamor. Earlier this month, the <i>New York Times</i> warned in a front-page headline, "U.S. Says Iran-Supplied Bomb Is Killing More Troops in Iraq."<p>
The <i>Times</i> article quoted Lt. Gen. Raymond Odierno, the second-ranking U.S. commander in Iraq, repeating the administration claim that deadly bombs called explosively formed penetrators (EFPs) were being provided by the Iranian government--though U.S. forces have reported discovering workshops to manufacture these devices in Iraq.<p>
By what criteria should Iran rank among the "global terrorists"? If the litmus test is nuclear weapons capability, then the U.S. government is the worst terrorist organization of them all--it has more nukes than anyone else, and it's the only government in the world to use them.<p>
If the test is terrorizing neighboring countries, then Bush should put Israel on the list--for its barbaric air war against Lebanon one summer ago, among many other example.<p>
And as for providing weapons to carry out terror, just a few days after news hit of the Guards' "terrorist" status, the U.S. signed a deal with Israel to provide an additional $30 billion in military aid over the next 10 years.<p>
The Bush administration is recycling old lies to try to pave the way for war on Iran. We have to oppose them.<p>

