<font face="Times New Roman, Times, serif" size="5"><b>Defending a reclaimed workplace in Argentina</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">August 24, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">ON AUGUST 23, thousands of social movement activists will rally at the Hotel Bauen in the heart of Buenos Aires to protest a court-ordered expulsion of workers from their workplace.<p>
The hotel is a successful example of one of Argentina's "reclaimed workplaces," where workers took over the workplace after their bosses abandoned it and have run it since under a workers' cooperative. Now, armed with a court order, the former corporate owners, Mercotel S.A., want to expel the workers and re-establish their control.<p>
The story of the Hotel Bauen encapsulates well the entire movement of reclaimed workplaces in Argentina.<p>
The hotel was built with subsidies from the country's military dictatorship in 1978 to cater to tourists when Argentina hosted the World Cup soccer championship. The hotel's politically connected original owner never repaid the government's subsidy, nor paid taxes. Instead, he just pocketed the hotel's revenues, while ignoring hotel upkeep. A Chilean firm that bought the hotel in 1997 followed the same pattern.<p>
During the country's financial crisis in 2000-2001, the owners declared bankruptcy, throwing the hotel's 80 workers out on the street. Instead of accepting their fate, the Bauen workers organized themselves to "reclaim" the hotel. In 2003, they formed a workers' cooperative and retook the hotel. When they entered the place in 2003, they found it empty and in disrepair.<p>
In the four years since taking over the hotel, the Bauen workers have remodeled it, and they have been able to expand the staff to 150. A workers' assembly makes decisions for the hotel, which includes a restaurant, a theater, a bookstore and meeting rooms.<p>
More than simply an example of how workers saved their jobs, Hotel Bauen has also become a cultural center for the social movements, hosting meetings and conferences, as well as cultural festivals and theater performances. Recently, the government of Venezuela signed an agreement with the cooperative to promote tourism in cooperation with the hotel.<p>
Now that Argentina has recovered from its economic collapse, and the international travel industry is touting Buenos Aires as the "next big thing," the owners who left the hotel in ruins want to take it back from the workers who revived it. In July, the owners received a favorable ruling from a judge, who ordered the workers to hand over the hotel by the end of August.<p>
The Bauen workers and the Argentine social movements aren't going to let the Bauen go down without a fight. As Hebe de Bonafini, the spokesperson of the human rights group the Mothers of the Plaza de Mayo put it, "We have to defend them with all our might." In the last four years, the workers have successfully fought off other attempts to expel them.<p>
The Bauen workers are asking for international support by means of <a href="http://www.petitiononline.com/bauen/" target="_blank">a petition to Argentine President Nestor Kirchner</a>, asking him to end all threats of expulsion and to pass legislation expropriating the hotel on behalf of the workers' cooperative. Readers can join figures such as authors Naomi Klein, Noam Chomsky and Arundhati Roy in signing the petition.<p>
As the petition explains, "Social movements around the world are watching the struggle of the Bauen Hotel workers with great interest and passionate support. This highly successful alternative should not be destroyed or threatened: It should be celebrated, supported, and shared with others!"<br>
<b>Lance Selfa</b>, Chicago<p>

