<font face="Times New Roman, Times, serif" size="5"><b>New York teachers' union concedes on merit pay</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Peter Lamphere and Megan Behrent, United Federation of Teachers</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 26, 2007 | Page 14</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--The United Federation of Teachers (UFT) have agreed to a form of merit pay that will greatly undermine union principles and weaken the teachers' unions fight against similar proposals in legislation currently before Congress.<p>
Under the new agreement for schoolwide "bonus" plans, teachers at the lowest performing schools in New York City would be offered merit pay in exchange for improved test scores and reaching other achievement benchmarks set by the Department of Education.<p>
At a UFT delegates' assembly October 17, UFT President Randi Weingarten argued that these bonuses will shut the door to more insidious plans for individual merit pay, and would promote collaboration among teachers.<p>
In fact, the plan will foster divisions within schools and weaken the union. As the <i>New York Post</i> says, "clearly, the camel's nose is under the tent" for the type of merit pay proposed in the renewal of the No Child Left Behind (NCLB) Act, now being debated in Congress.<p>
The UFT's parent union, the American Federation of Teachers, along with the National Education Association, have resisted merit pay schemes. But now, the UFT has accepted merit pay by another name, and placed an incredibly divisive tool in the hands of New York City Schools Chancellor Joel Klein and the principals.<p>
Here is how it will work:<p>

<blockquote>
-- Some 200 high-needs schools (about 15 percent of those in the system) will be invited to participate. The number will double next year if it passes an independent review.<p>
-- A 55 percent majority of UFT members in those schools must vote to accept the plan. However, participation in the plan will be a mitigating factor against closing a school, thus holding the threat of closure over the heads of our members if they vote "no."<p>
-- Once a school accepts the plan, it will be allocated an amount equal to $3,000 per UFT member if it meets the city's benchmarks for testing.<p>
-- This money will be distributed by a "compensation committee," made up of a principal, his or her designee, and two UFT members elected by the staff. If the committee does not reach consensus on how to distribute the money, or the staff votes down its decision, then the bonuses are forfeit. This means that a principal can insist on a particular method of distribution (by test scores, etc.), and the staff must agree, or lose the money.
</blockquote><p>

At the delegates' meeting Kit Wainer, a Brooklyn UFT chapter leader who ran against Weingarten on an opposition slate last year, pointed out how the merit pay agreement would divide teachers and lead to finger-pointing about who wasn't carrying their weight within a school.<p>
There were a few speakers in favor of the proposal, mostly on the basis that it helped the union reach an agreement with the city to jointly lobby the New York state legislature to allow teachers to retire at age 55 with 25 years of service, rather than the current 30 years.<p>
One delegate moved to table the issue so delegates would have an opportunity to discuss it with the rank and file, but this was ruled out of order. In a confusing rush, the delegates then voted for the plan by a large majority. A proposal for a national teachers' demonstration against individual merit pay was voted down by delegates, although Weingarten promised to contact the AFT about the possibility.<p>
However, thanks to Weingarten's "bonus" plan, merit pay is now a reality in the New York Public Schools. The delegates' assembly meeting was a historic turning point in the history of our union--for the worse.<p>

