<font face="Times New Roman, Times, serif" size="5"><b>A keen sense of injustice</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 26, 2007 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">WE IN Austin are mourning the loss of our dear comrade, Quent Reese, who tragically took his own life after a long struggle with depression.<p>
Quent had a very basic sense of the injustice of the world that marked his whole outlook. He could argue the case for a different and better kind of world in terms that were persuasive, but not doctrinaire. And there was no gap between Quent's beliefs and his practice. He brought a professional flair and dedication to the ISO and the Campaign to End the Death Penalty (CEDP), perhaps his favorite cause.<p>
Quent joined the ISO in late 1990s while a student at the University of Texas. He cut his activist teeth in the UPS strike and a mass anti-racist student movement. As a fairly new activist, he preceded Jesse Jackson in a speech to a crowd of 5,000 students rallying against an anti-affirmative action law professor.<p>

