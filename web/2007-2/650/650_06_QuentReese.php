<font face="Times New Roman, Times, serif" size="3">Quent was among a group of activists arrested at a sit-in at then-Gov. George Bush's mansion in the struggle to save Gary Graham/Shaka Sankofa from execution. From the beginning, Quent has been dedicated to the cause of saving Texas death row prisoner Rodney Reed.<p>
The first CEDP member to be a pen pal to Kenneth Foster Jr., Quent brought the peculiar details of the case to the group. He recognized Foster as a fellow revolutionary and helped launch the movement that saved Foster from execution last summer.<p>
Quent was gently persuasive, but when called for, he had a sharp combative streak. He wasn't afraid to stand up to power or someone defending injustice. Quent will also be missed for his sensitivity and funny, dry sense of humor.<p>
Quent was one of the good ones. A dear friend, he will always be remembered and sorely missed.<br>
<b>Cindy Beringer, Mike Corwin and Lily Hughes,</b> Austin, Texas<p>

