<font face="Times New Roman, Times, serif" size="5"><b>Deflecting attention from police torture</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 26, 2007 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">STANLEY HOWARD was tortured by Chicago police under the command of Jon Burge, and his "confession" was used to send him to death row. In January 2003, Stanley was pardoned by then-Gov. George Ryan, though he remains behind bars today because of another wrongful conviction. Here, Stanley writes on the authorities' latest maneuver to dodge responsibility in the Burge torture scandal.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">HAVING SUFFERED immensely for the last 23 years on death row and behind prison walls, I know firsthand how racist, unjust and corrupt the criminal justice system is.<p>
So obviously, I was overwhelmed with pride and joy seeing tens of thousands of people march on Jena, La., on September 20. They traveled from all around the country to express support for the six Jena juveniles who were being persecuted on trumped-up charges of attempted murder, which stemmed from a minor schoolyard fight.<p>

