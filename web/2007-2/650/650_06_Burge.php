<font face="Times New Roman, Times, serif" size="3">The Jim Crow-style injustice of the Jena 6 case illuminated the double standards in the system for Blacks and whites, and the call for action was sent out through blogs, Web sites and the Internet--a new way of fighting an old enemy in a new era.<p>
The march seemed like it was serving a dual purpose--a call for justice for the Jena 6 and a launching pad for what Revs. Al Sharpton and Jesse Jackson, and Minister Louis Farrakhan call "the most major civil rights issue of our time"--the issue of the criminal "just-us-African-Americans" system.<p>
It took less than a week for the new developments in the Burge torture scandal to turn my joy back to anger and show the reality that it's going to take a similar march on Chicago to extract justice for the torture victims.<p>
Last year, special prosecutors concluded in a $7 million report that fired police Commander Jon Burge and his underlings tortured criminal suspects for two decades. But they claimed it was too late to prosecute the torturers because the statute of limitations has since run out.<p>
Believing that the report was a sham and that it wasn't too late to prosecute, five city aldermen sent a letter to the U.S. Attorney on the same day of the Jena 6 march. "We strongly believe that the federal prosecution of Burge and police helpers is possible, and that the statue of limitations is not a bar," the aldermen wrote. "Burge and all those involved can be prosecuted for perjury, obstruction of justice and the ongoing conspiracy to cover up their torture scheme."<p>
A recently released report estimated that if the city insists on challenging the lawsuits filed by five of the torture victims, the city could end up paying between $96 million and $195 million.<p>
Based on this grim estimate, all major Chicago newspapers and six aldermen demanded on September 25 that Mayor Richard Daley honor the $14.8 million settlement reached late last year with Leroy Orange, Stanley Howard and Madison Hobley, and reach agreements with Aaron Patterson and Darrel Cannon.<p>
Trying to explain why Mayor Daley lied right before the mayoral election that "no settlement was reached," city attorneys distributed a mindblowing letter on September 26 that they received from the U.S. Attorney's Office late last year, stating that it was investigating the Madison Hobley case.<p>
Hobley was wrongfully tried and convicted of murder and sent to death row. He received a full pardon based on innocence from then-Gov. George Ryan in 2003. And considering the laws against double jeopardy that prevent people from being tried twice for the same offense, there's no way the Justice Department <i>could </i>retry him.<p>
Instead of investigating the largest police corruption scandal in U.S. history, in which over 150 Black men were sadistically tortured by a group of racist cops, they had the audacity to investigate the Hobley case. Only in America could this happen.<p>
I really shouldn't be so surprised at this latest revelation, because this is the same government that condones the use of torture to obtain confessions and information, and the same racist government that targets Blacks for jail, instead of Yale. With 24 torture victims still incarcerated and the Feds targeting Hobley instead of Burge, Jim Crow is alive and well in Chicago.<p>

