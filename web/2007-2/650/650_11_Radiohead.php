<font face="Times New Roman, Times, serif" size="5"><b>Cutting out the greedy record labels</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Alexander Billet</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 26, 2007 | Page 11</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Radiohead, <b><i><a href="http://www.inrainbows.com" target="_blank">In Rainbows</a>, </b></i>2007.</font><p>

<font face="Times New Roman, Times, serif" size="3">ON OCTOBER 10, Radiohead released their seventh record, <i>In Rainbows</i>. It is an album highly anticipated, their first since 2003's <i>Hail to the Thief</i>. It was released without a record label. Fans can download it through a Web site run by the band. The price? As much as you feel like.<p>
That's right. Fans have access to new music by one of the world's biggest and most popular bands, for as much or as little money as they desire, including nothing.<p>
A disc box including CD and vinyl versions as well a bonus album will be released in December, but in the meantime, there are no parasitic record labels involved, no exorbitant CD prices. Just a direct channel between artist and listener.<p>
Ever since 1997's <i>OK Computer</i>, the group has been well-known for breaking the musical mold. While the stale, amorphous concept of "post-grunge" seemed to be the direction most rock music was going in, <i>OK Computer</i> dared to mix spacey electronica, abstract avant-garde and more traditional rock instruments into an artistic range that went well beyond the confines of the mainstream.<p>
After their contract with mega-label EMI expired, they opted to record their album without re-signing. Their disdain for the traditional music industry was made humorously obvious by singer Thom Yorke: "What we would really like is the old EMI back again, the nice genteel arms manufacturers who treated music [as] a nice side project who weren't too concerned with the shareholders. Ah well, not much chance of that."<p>
If Radiohead have always sought to shatter convention, perhaps that's because they find convention so incredibly repressive. Their expansive sound has always had a surreal menace to it, senses of both despair and abject panic that push back against each other with teeth-grinding harshness.<p>
Their lyrics extract as much from Naomi Klein's <i>No Logo</i> (which they have cited as a political influence) as they do from dystopian science fiction (they are fans of new wave sci-fi writer J.G. Ballard).<p>
When these influences spring forth, the result is a feeling of absolute foreboding, the idea that dystopia could be right here, right now. <i>In Rainbows</i> continues this in this fashion, while also defying expectations of how it was "supposed" to sound. The album is, in Yorke's own words "embarrassingly minimalistic," while still employing most of the elements of their past albums, chiefly their experimentation with dissonance and electronica.<p>
The frantic "Bodysnatchers" builds toward a desperate ending as Yorke's disturbing yet beautiful voice tries to escape something--everything--in total futility: "Have the lights gone out for you/'Cause the light's gone out for me/This is the 21st Century...You can fight it like a dog/And they brought me to my knees...All the lies run around my face/And for anyone else to see."<p>
"House of Cards" is a deceptively lovely track. On the surface it is a love song, until we hear references to infrastructure collapsing and voltage spikes. Beneath the surface of this pristine world, there is something very sinister and frightening.<p>
<i>In Rainbows</i> is an attempt to break free from that world. Its model has proven popular. Within two days, the album went platinum (the average purchase price was $8). Successful groups like Oasis and Jamiroquai have already announced they will release future records in similar fashion. These groups can most certainly afford it, but the precedent still stands.<p>
In short, Radiohead, long known for breaking boundaries, have opened a floodgate with this album. If it's opened wide enough, who knows what might be there when the waters settle?<p>

