<font face="Times New Roman, Times, serif" size="5"><b>Revolt over UAW's Chrysler deal</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 26, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>LEE SUSTAR reports on widespread opposition to concessions in the tentative Chrysler contract.</b><p>

RATIFICATION OF a proposed concessionary union contract at Chrysler was in doubt as <i>Socialist Worker </i>went to press.<p>
Despite arm-twisting by leaders of the United Auto Workers (UAW), workers at six union locals voted to reject the deal--which contains not just the same concessions in the recently approved UAW contract with General Motors, but worse.<p>
The final outcome of the vote will depend heavily on a vote at Chrysler plants in Belvidere, Ill., and Sterling Heights, Mich., where the local presidents have not endorsed the deal.<p>
Like the GM contract, the tentative agreement at Chrysler, reached after a token six-hour strike October 10, calls for cutting wages in half for all "non-core" jobs--that is, for workers not directly involved in assembly line production.<p>
At GM, this was understood to mean pay cuts for future hires in jobs like materials handling, janitorial work and jobs in parts warehouses.<p>
In the Chrysler deal, however, the lower-tier wages would also be instituted for all jobs in three key manufacturing plants--Toledo Machining, Detroit Axle and Marysville (Mich.) Axle. Workers at 19 parts depots will also be designated as "non-core," as will transport workers. As at GM, all new hires would no longer get pensions, but a 401(k) plan instead.<p>
And where GM offered full-time jobs for about 3,000 temporary workers, Chrysler refused to do so. That was a key reason why UAW Local 1268 President Thomas Littlejohn, who represents workers at the Belvidere plant, refused to endorse the deal. Out of a workforce of 3,800 in the plant, some 600 are temps.<p>
Further, Chrysler failed to commit to substantial investments in U.S. plants, as GM had done.<p>
The two-tier wage provision was the main reason why Local 1700 President Bill Parker of the Sterling Heights plant also opposed the deal. Parker, elected as chair of the Chrysler bargaining committee, broke with UAW President Ron Gettelfinger and other top union officials to call for a rejection of the deal at a meeting of the UAW's Chrysler Council, comprised of leaders from Chrysler plants.<p>
The meeting was stormy, and opposition was greater than what was reported by UAW leaders. According to Shawn Fain, a skilled trades committeeman in Local 1166 in Kokomo, Ind., his motion for a roll call vote at the meeting was ruled out of order in order guarantee passage of the deal.<p>
"I made a motion for a roll call vote so that we could have an exact count to report to our respective local memberships," Fain wrote in a report to his local. "According to Jim Coakley, who chaired the council meeting, my motion was defeated. Again, this vote was a total sham, due to the fact that there was no way to get an accurate count of those voting in favor or opposed, and the fact that there are numerous people in the council meeting who have no voice, yet they yell out their vote anyway."<p>
Another reason for opposition to the deal is the proposal to eliminate most classifications for skilled trade workers--that is, forcing electricians, millwrights and others to do one another's work and reducing the number of these well-paid jobs.<p>
Like the GM contract, the Chrysler deal would create a union-controlled Voluntary Employee Beneficiary Association (VEBA), which would take over retiree health care costs. But where GM will under fund the VEBA at 70 percent of its liabilities, the Chrysler VEBA will be funded at just 50 percent. "In reality, it's $7.1 billion cash for $19 billion in liability," wrote Gregg Shotwell, a GM worker and an activist in the Soldiers of Solidarity opposition network.<p>
A public memo on the Portfolio.com Web site to Chrysler CEO Robert Nardelli spelled out the benefits of the contract to the management of Chrysler, now owned by the private equity firm Cerberus.<p>
"[The UAW] pretended to strike, and you pretended to cave," reads the memo. "In reality, the $11 billion you paid to get the health-benefit liabilities off your books will soon look outrageously cheap, and limiting job guarantees to the lifetime of the individual products will give you far more maneuverability than anyone is reporting."<p>
Despite the lack of reporting, a large number Chrysler workers recognize the deal for what it is--an unprecedented capitulation to management.<p>

