<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">October 26, 2007 | Page 14</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Wisconsin state workers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By John Carey and Julio Rodriguez</b></font><p>

<font face="Times New Roman, Times, serif" size="3">MADISON, Wis.--Nearly 2,000 trade unionists, progressives and leftists turned out for a rally October 17 to demand that the state pass its budget. Without a budget, state workers face the threat of layoffs and a loss of services at crisis levels.<p>
AFSCME, the American Federation of Teachers and the Wisconsin AFL-CIO brought busloads of unionists and allies from around the state.<p>
About 400 members of the right-wing "Americans for Prosperity" group also rallied and provided cover for the legislators' foot-dragging tactics. Anger on both sides was high--the unionists presented a clear and controlled voice demanding an end to the budget impasse.<p>
Hopes were raised earlier in the year when the state senate proposed a progressive health plan to cover all Wisconsin workers. Although the plan was quickly taken off the table in early negotiations, it was still on people's minds. A member of International Brotherhood of Electrical Workers Local 494 from Milwaukee questioned why a single payer health care system was not available.<p>
One activist described the lawmakers as having "no regard for the needs of seniors, children or workers. The rich should be taxed by closing loopholes for banks, insurance companies and corporations."<p>

