<font face="Times New Roman, Times, serif" size="5"><b>Old excuses from the new majority</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 26, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>LANCE SELFA points out that congressional Democrats are still claiming they "don't have the votes" to stop Bush--even though they are the majority.</b><p>

IT WAS only about a year ago that the Democrats, riding a wave of antiwar sentiment and discontent with the Republican Congress, ousted the GOP to take control of the legislative branch of government. It appeared that the Bush administration might finally be called to account for its disastrous policies, from Iraq to New Orleans.<p>
Yet a year later, it doesn't feel like much has changed. Remarking on the Democrats' likely cave-in to Bush's demands that they permanently legalize a full array of warrantless wiretapping, even the <i>New York Times</i> sounded disgusted:<p>

<blockquote>
Every now and then, we are tempted to double-check that the Democrats actually won control of Congress last year. It was particularly hard to tell this week. Democratic leaders were cowed, once again, by propaganda from the White House and failed, once again, to modernize the law on electronic spying...<p>
It was bad enough having a one-party government when Republicans controlled the White House and both houses of Congress. But the Democrats took over, and still the one-party system continues.
</blockquote><p>

If the <i>New York Times</i> feels frustrated, what about the millions of Americans who hoped a Democratic Congress would finally put a brake on the White House?<p>
The sentiment against the war has only increased since last Election Day. Seventy percent of Americans oppose President Bush's handling of Iraq, according to a recent CBS poll.<p>
But at the same time, approval levels for Congress have returned to where they were before the Republicans lost control. So whatever initial hopes the public invested in the Democrats and Congress have largely disappeared.<p>
How can the Democrats be so oblivious to an obvious mandate? A small glimpse into their thinking came to us via the transcript of an August conference call with leading liberal antiwar organizers and two members of Congress, Reps. Lynn Woolsey (D-Calif.) and Jim Moran (D-Va.), posted on the Web site of the Network of Spiritual Progressives, the conference call host.<p>
During the call, Woolsey, a favorite of liberal antiwar activists, and Moran, the mercurial congressman whose district includes the Pentagon, explained the obstacles they face in voting to do what the vast majority of Americans want them to do--end the war in Iraq.<p>
"The Democrats are more afraid of being labeled as abandoning [the troops]," Woolsey says. "If we fund the safe, orderly redeployment, then indeed folks will start phrasing it that way, including our own leadership. The Republicans will say it, but Democrats have to quit saying it."<p>
The sponsor of the conference call, Rabbi Michael Lerner, keeps returning to one point: Why doesn't Speaker Nancy Pelosi refuse to schedule a vote on any appropriation for the Iraq war other than one to bring the troops home? That's Pelosi's decision, Woolsey says, and she's unlikely to take any other course.<p>
Code Pink activist Medea Benjamin complains that Pelosi won't even meet with her antiwar constituents in San Francisco. But both Woolsey and Moran take pains to assure activists that "I believe Nancy is with us, and she's counting on you and Barbara and Maxine and me to push from the left in Congress," Woolsey says, referring to Reps. Barbara Lee (D-Calif.) and Maxine Waters (D-Calif.).<p>
Moran agrees: "The Speaker doesn't have the votes. If you see what has happened in the Democratic caucus, I don't think you'd be quite as critical of the Speaker. She really is trying. She doesn't have the votes; she doesn't even have the complete support of the some of the leadership."<p>
Moran then explains that some Democratic leaders don't want to push on the war issue because freshmen representatives elected in more conservative districts in 2006 might lose their seats.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">IF ANYTHING explains the sense of drift and demoralization pervading the antiwar movement today, the litany of excuses from Woolsey and Moran are a good place to start.<p>
When the Democrats were in the minority, they used to explain their cave-ins to Bush on the grounds that they "didn't have the votes" to hold the administration accountable. Now they're in the majority--and they still "don't have the votes."<p>
On the call, Benjamin complains to Moran that Pelosi's strategy isn't working. "It'd be nice if she sat down with us so maybe we could work with her in a different way," Benjamin says. In response, Moran pulls out the Democrats' ultimate trump card with activists: "I know that, but in all fairness, until we get a Democratic president, until we get a president who is committed to ending the war..."<p>
After disappointing supporters in the aftermath of 2006, the Democrats are asking for another chance--this time in 2008. But if antiwar activists set their sights on getting a Democrat in the White House, they're setting themselves up for an even bigger betrayal.<p>
All three leading contenders for the Democratic nomination have refused to pledge to get troops out of Iraq even by 2013. The frontrunner, Hillary Clinton, distinguished herself by voting <i>for </i>a bill that can open the door to war with Iran and becoming the favorite candidate of the U.S. arms industry.<p>
When one considers those facts, perhaps the Democrats' congressional stiff-arm to public opinion isn't as hard to understand as it appears. Rather than being cowards, perhaps they're acting as caretakers who feel confident that they and their party will assume full control over the war in Iraq and U.S. foreign policy in 2009.<p>
Rather than take any risks for "peace," they're planning to assume the role of managing current and future U.S. wars.<p>

