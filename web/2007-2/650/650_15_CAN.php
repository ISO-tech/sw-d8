<font face="Times New Roman, Times, serif" size="5"><b>Antiwar students map out strategies</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Wrigley-Field</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 26, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">MADISON, Wis.--Members of the Madison community joined some 100 students from 25 schools for a students and veterans panel discussion on October 21 that featured Camilo Mej&iacute;a, the national chair of Iraq Veterans Against the War (IVAW) and the first soldier to publicly refuse to redeploy to Iraq.<p>
The discussion was the evening plenary session of Students Rising, the fifth national conference of the Campus Antiwar Network (CAN).<p>
Mej&iacute;a explained how his experiences in Iraq--starting with his assignment to take part in the abuse of Iraqi prisoners under the direction of private contractors--led him to serve a military prison sentence rather than continue his role in the occupation.<p>
"When you live in a country that places profit above human needs, you live in a country that needs blind patriotism," Mej&iacute;a said. "Instead of being American or being patriotic, we need a sense of being human beings all together. It becomes very difficult to play a role as occupier when you see the Iraqi people as human beings."<p>
Mej&iacute;a was joined on the panel by Jean Brody, a freshman at the University of Wisconsin, who discussed her own experience becoming a CAN leader this semester.<p>
Also answering questions was fellow IVAW board member Liam Madden, who is a CAN activist at Boston's Northeastern University. At the conference's opening plenary, Madden challenged the major justifications for staying in Iraq. "What's going on is consistent with U.S. foreign policy for the last 100 years," he said. "It's divide and rule. It's how occupations work."<p>
Madden also urged students to use their time at school to begin a lifetime of political activism, saying, "You have the opportunity to leave college with a stronger infrastructure of the antiwar movement--and go into the next phase of your life with some experience as an organizer."<p>
Mej&iacute;a and Madden both highlighted the key alliance between CAN and IVAW, especially for creating campus antiwar chapters and challenging military recruitment. With this in mind, CAN delegates voted to create a national committee focused on organizing with veterans on campuses.<p>
Conference delegates also voted to begin collaboration with the Iraqi Student Project. At selected schools, CAN chapters will campaign for Iraqi students who have been made refugees by U.S. bombs to be admitted tuition-free.<p>
The conference also elected a CAN coordinating committee; called for a national week of student protest for the fifth anniversary of the war, plus regional student antiwar conferences in the spring; and approved a campaign against the threat of war with Iran.<p>
<i>For a full report on the CAN conference, go to <a href="http://www.campusantiwar.net" target="_blank">www.campusantiwar.net</a></i>.<p>

