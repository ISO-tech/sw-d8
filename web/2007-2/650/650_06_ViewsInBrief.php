<p><a name="October"></a><font face="Times New Roman, Times, serif" size="4"><b>Mobilizing for October 27</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WE ARE writing to express our disappointment with Sharon Smith's article on the October 27 regional demonstrations called by United for Peace and Justice (UFPJ) ("Too late and too little," October 19).<p>
We certainly agree with her assessment of the increasing hostility of the Democratic Party establishment to the antiwar movement and the UFPJ leadership's disastrous "building a peace bloc in Congress" strategy. Yet Smith's article itself is "too little and too late."<p>
You did not address the UFPJ October 27 demonstrations that are shaping up in the Northeast and in the West Coast. While your criticism of the backdoor maneuvering in the Chicago UFPJ and the invitation to pro-imperialist politicians is both principled and valid, it is not representative of what is happening elsewhere.<p>
The Boston demonstration, for instance, is organized by New England United, which is comprised of forces broader than the UFPJ coalition, including left and liberal forces. The demonstration has been sponsored and endorsed by mainstream liberal organizations, including trade unions across the region.<p>
Unlike Chicago, a series of democratic discussions were held to decide the character of this demonstration, which among other things has put IVAW veterans at the front of the march, has many leading Arab voices represented at the platform, and is supportive of the Sabeel Conference on Palestine happening in Boston on the same day. Indeed, International Socialist Organization (ISO) members have been essential contributors to the proceedings of New England United.<p>
By a blanket denunciation of UFPJ, and not distinguishing between different regional coalitions, the article tarnishes some good grassroots work done in many areas around the country under the October 27 banner, which can serve as a model for the future. Our understanding is that a similar effort is underway in the Bay Area. Your narrow focus on Chicago didn't address these developments.<p>
Secondly, we question the poor timing of this article. If your criticisms of UFPJ's failure to call for a national demonstration had appeared prior to the war-funding vote in Congress, it would have been more useful. But we also wonder why the ISO did not mobilize for the one demonstration that did coincide with the Petraeus report and the war-funding vote, called by ANSWER?<p>
It is irresponsible, with two weeks left until the October 27 demonstrations--when activists have spent tremendous amount of time and energy organizing for it--for the ISO to not put an all-out call to mobilize for the demonstrations in the East and West Coasts. We encourage all your readers to attend the October 27 protest in Boston! <br>
<b>Khalil Iskarous</b> and <b>Naveen Jaganathan</b>, Boston<p>

