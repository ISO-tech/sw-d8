<a name="SF"></a><font face="Times New Roman, Times, serif" size="4"><b>San Francisco's homeless crisis</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WALKING THROUGH downtown San Francisco, as I do many nights on my way to work, is truly heartbreaking. There is such a stark juxtaposition of exorbitant wealth right next to utter squalor.<p>
Recently, the <i>San Francisco Chronicle</i> has run multiple front-page articles in a series offensively titled, "Homeless Special: The Shame of the City." Some articles in the series include, "'Enough is enough,' SF says of homeless" and "SoMa police patrol to shepherd homeless away from tourist areas."<p>
There has been ramped-up enforcement of "quality-of-life crimes"--a sugarcoated name for the criminalization of homelessness. The articles in the <i>Chronicle</i> basically argue that heavy police enforcement is the solution to the widespread problem of homelessness. It infuriates me.<p>
Temporarily throwing homeless people into California's already massive prison system, only to release them back into a life of poverty and homelessness a few months later, is no solution at all. And while I strongly support creating more shelters and services for the homeless instead, I do not see this as a complete solution either.<p>
The same newspaper that condemns individuals for being homeless runs articles about the mortgage crisis and how Bay Area foreclosures have tripled since last year (effectively throwing even more people to the gutters of San Francisco).<p>
Homelessness hasn't developed because there aren't enough houses for people to live in. In fact, quite the opposite. Right now, in the midst of the housing bubble bursting, there are too many houses--but they cannot be sold for profit, so poor working people (and all too often veterans) are thrown to the streets. Ultimately, homelessness is inextricably wrapped up in a system that thrives on profit.<p>
Homelessness is an absurd problem that abounds in one of the richest countries in the world. The recent attacks on the homeless in my city have reminded me why we need to continue to fight for a world where access to adequate housing is a basic human right.<br>
<b>Kristin Lubbert</b>, San Francisco<p>
