<font face="Times New Roman, Times, serif" size="3">A coworker, Dave, agreed. "Every time they give us a raise, they cut down our hours," he said. "We need to improve, but we always stay in the same spot."<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">IN ALAMEDA County, in Northern California, Waste Management workers beat the company's union-busting tactics this past summer, in a tough 26-day lockout that saw active picket lines and widespread union solidarity. Now, the battle is on in Southern California.<p>
The average worker makes $17.80 an hour. That comes out to $37,000 a year before forced overtime. Because raises are based on percentage increases, the gap between pay for higher and lower-paid WM workers (who do the same jobs) is getting bigger, not smaller. Local 396 President Jay Phillips said the strike is as much about "how the workers are treated" as about wages.<p>
The wage dispute alone, though, is reason enough to support the workers' cause. WM is offering a five-year contract with raises of $1 per hour in the first year, followed by raises of 70 cents, 65 cents, 65 cents and a dollar in each of the following years. This means that it would take five years for workers to be making an average of $21.80 an hour--far too low given the high cost of living in Los Angeles County.<p>
Herrera explained some of the larger issues at stake for the heavily immigrant workforce. "WM has a big new corporate campaign, called 'Think Green,'" he said. "They are supposed to be a big environmentally friendly company. But I could take you to facilities around here where they treat the workers like a Third World country...<p>
"They play the psychological card against the immigrant workers, making them think that there will be raids and deportations if the workers speak out. They try to create tension between the Black and Latino workers."<p>
According to Phillips, WM also maintains a professional strikebreaking operation, called the Green Team, which is made up of management employees who jet to the scene of any labor dispute and run scab trucks. Herrera describes the Green Team as "our Blackwater," after the notorious private security firm at work in Iraq.<p>
Rank-and-file workers have a lot to say about what it would take to win this strike. Luis Fregoso, a 10-year WM employee from a different Teamsters local who was out picketing with the strikers, said, "We've got to block them from doing regular business." He estimated that by the second day of the strike, about 10 percent of the garbage routes were being run by scab workers.<p>
Another challenge for the strikers is the fragmentation of union agreements in the industry, Frego said. "Even though you have only a very few companies handling all the trash business, we're not like UPS. We're all in different locals. Every hub has to organize itself, and the unions compete to organize them."<p>
Strikers say that the new leadership of Local 396 which took office a few years ago was more willing to stand up to management.<p>
"We're definitely more aggressive than our predecessors," Phillips said. "The last strike of this local was in 1979."<p>
But the strike authorization vote carried by 247 to 115, so some of the workers clearly still need to be won over. Meantime, Local 396 leaders seem encouraged that Los Angeles Mayor Antonio Villaraigosa' s office started helping to mediate negotiations as the strike began. But despite the mayor's background as a union negotiator, he's likely to put pressure on the union to settle quickly for political reasons, even if it means concessions for workers.<p>
The labor movement needs to mobilize to support the WM workers to defend their union.<p>

