<font face="Times New Roman, Times, serif" size="5"><b>Teamsters strike Waste Management In LA County</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sarah Knopp</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 26, 2007 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">SOME 500 garbage collectors in Los Angeles County went on strike against Waste Management October 19 in a dispute over wages and respect at the workplace.<p>
The strike affects much of LA County, the city's suburbs and unincorporated Los Angeles, from the San Fernando Valley to Long Beach, although not the city of Los Angeles itself.<p>
The workers, members of Teamsters Local 396, are up against the largest trash hauler in the U.S. Waste Management (WM) makes an average annual profit of $24,000 a year per employee, according to Local 396 Secretary-Treasurer Ron Herrera. The mega-corporation netted more than $1.1 billion in profits last year.<p>
For employees, though, life isn't so rosy. "We work 12- to 13-hour days," explained Gilberto, an employee walking the picket line in LA's industrial suburb of Carson. "We come in at 3 a.m. or 5 a.m. I have two little girls, and I'm not spending time with them."<p>
Management keeps up the pressure, Gilberto said. "They have safety meetings once a week," he said, "and they tell us that we're 'professionals'--that we're better than BMI [another major waste collection company]. The word that they use is 'professionals,' but they don't pay us like professionals."<p>

