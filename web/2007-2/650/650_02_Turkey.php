<font face="Times New Roman, Times, serif" size="5"><b>U.S. scrambles to contain Kurdish-Turkey conflict</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 26, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">U.S. DIPLOMATS were scrambling to keep Turkey from launching an offensive into Kurdish-dominated northern Iraq, as <i>Socialist Worker </i>went to press.<p>
On October 21, Kurdish guerrillas launched their boldest offensive against Turkish troops in many years, killing 17 and taking eight hostage. Four days earlier, Turkey's parliament had raised the stakes in the tense standoff by voting overwhelmingly for a one-year mandate for the Turkish military to carry out strikes in northern Iraq to root out fighters from the Kurdish Workers Party (PKK).<p>
Already, 60,000 Turkish troops are on high alert on Iraq's border, awaiting orders to go ahead with the operation. A convoy of 50 more armored military vehicles made its way to the border after the latest attack by Kurdish guerrillas.<p>
A Turkish invasion of northern Iraq would present the U.S. with a new disaster. Not only would it mean open conflict between two of the most important U.S. allies in the region, but it would cause chaos in the one part of Iraq considered to be stable.<p>
Secretary of State Condoleezza Rice personally called Turkish Prime Minister Recep Tayyip Erdogan after the attack to plead with him to wait "a few days."<p>
For decades, world powers have denied an independent state to the Kurdish people, whose population spans northern Iraq, southeastern Turkey, eastern Syria and western Iran.<p>
The U.S. is merely the most powerful country to cynically manipulate Kurdish national aspirations. It supports Kurdish "autonomy" in northern Iraq while opposing the PKK in Turkey. The U.S. has placed the PKK on its list of terrorist organizations, but hasn't taken concrete steps to curb its activities.<p>
Since 1991--when the U.S. turned on its former ally Saddam Hussein and began nearly two decades of military and economic warfare on Iraq--leaders of the Kurdish national movement have collaborated with U.S. imperialism in Iraq.<p>
This has meant a tense balancing act with Turkey, which has regularly threatened--and several times carried out--military action against PKK bases in northern Iraq. Now Turkey is threatening an escalation of the conflict.<p>
The heightened tensions come in the wake of a House Foreign Affairs Committee vote to approve a resolution that brands as "genocide" the killing of 1.5 million Armenians by Ottoman Turks during the First World War.<p>
Leave it to George Bush to put in crass terms why the U.S. should continue to deny the historical fact of the Armenian genocide. "Congress has more important work to do than antagonizing a democratic ally in the Muslim world, especially one that's providing vital support for our military every day," he said.<p>
But after the committee vote, House Speaker Nancy Pelosi, who had spearheaded the resolution on Armenian genocide, backed down from bringing the resolution to a full House vote after Turkey threatened to bar the U.S. from continuing to use its Incirlik Air Base in eastern Turkey, which is critical for supplying U.S. troops in Iraq.<p>
Iran also has a Kurdish minority, which at times has gotten arms and support from the U.S. So Turkey might be tempted to look to the Iranian government for support--at precisely a time when the U.S. is working hard to isolate Iran.<p>
The tangled web of alliances and conflicts across the Middle East are being aggravated by the standoff in northern Iraq--with the threat that a Turkish incursion could lead to a wider war in the Middle East.<p>

