<font face="Arial, Helvetica, sans-serif" size="2"><b>MORE THAN A GAME</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Jocks 4 Justice in Jena</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 12, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>DAVE ZIRIN explains why an impressive group of sports-world figures made a statement of solidarity with the Jena 6.</b><p>

ONE OF the most rancid ideas we're spoon-fed from an early age is that politics is for experts only; ordinary people need not apply. Politics, they tell us, is for the people of Capitol Hill and their stenographers in the press.<p>
Forget about the fact that there are high school students who know more about the Middle East than some senators. Forget that the president, at a photo op hyping the renewal of his hideous No Child Left Behind education law, told a group of elementary school students in a prepared speech: "Childrens do learn." Forget that presidential candidate Fred Thompson spoke in an interview about how he would deal with "China and the Soviet Union" (one wonders what his plans are for Prussia).<p>
These are the "experts" we are supposed to entrust with health care, education and basic matters of war and peace. No wonder they dream their approval ratings will break into the 30s.<p>
Then there are the other geniuses "authorized" to talk about politics: people like Rush Limbaugh, Laura Ingraham and the other assorted ghouls on the right, as well as the Democratic Party's amen corner on Air America.<p>
Ingraham even wrote a book called <i>Shut Up &amp; Sing</i> demanding that celebrities and athletes to stop talking politics and leave it to the experts. (And what makes them experts? Did she and Limbaugh take some kind of special class in buffoonery? Does this class teach that "childrens" is a word?)<p>
That's why it's important to defend the efforts of people of the left like Tim Robbins and Michael Moore to try and influence political debate. When the political elites tell them to "shut up and sing," it's a way to tell all of us to go back to our iPods and mind our own damn business.<p>
There's a slight problem with their grand plans at pacification. That's the stubborn fact that, as much as they want to make political discourse the province of the Hill and their lobbyists, politics is connected to the food we eat, the air we breathe, the health care and education we have--or don't have.<p>
And politics breaks free from the confines of K Street most dramatically when people take to the streets and demand the kind of change that "official politics" never seems to deliver.<p>
This was seen most dramatically in Jena, La. Neither Hillary Clinton nor Barack Obama told 50,000 people to enter the town and demand justice for the Jena 6. Their actions reminded me of the words of Dr. Martin Luther King in his <i>Letter from a Birmingham Jail</i>, "There comes a time when the cup of endurance runs over, and men are no longer willing to be plunged into the abyss of despair. I hope, sirs, you can understand our legitimate and unavoidable impatience."<p>
The actions of these ordinary people have inspired some in the "shut up and sing" choir to step outside their box and be heard. Below are excerpts from a signature ad made up of people from the athletic community in solidarity with the Jena 6.<p>
What makes this group so interesting is that it combines familiar figures (athlete-activists like Washington Wizards center Etan Thomas and Dr. John Carlos) with new names like Saints linebacker Scott Fujita, ESPN.com writers Jemele Hill and Scoop Jackson, and even NBA union leader Billy Hunter.<p>
It's an impressive group, worthy of the even more impressive showing in Jena. This is about reclaiming politics from the "experts" who have so thoroughly failed us. Put simply, it's our turn.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="4"><b>Jocks 4 Justice Statement of Solidarity</b></font><p>
<font face="Times New Roman, Times, serif" size="3">WE, THE undersigned members of the sports community, call for the egregiously punitive criminal charges to be dropped against Mychal Bell, Carwin Jones, Theo Shaw, Robert Bailey Jr., Bryant Purvis and Jesse Beard, also known as the Jena 6.<p>
We don't call for this because all six young men are accomplished athletes in their own right, but because this case is drenched of the worst double standards and excesses of the criminal justice system.<p>
The story by now has become well known. It started in September 2006 at Jena High School in central Louisiana, where three lynching nooses were hung from a tree after Black students dared sit underneath what was known as "the white tree."<p>
Tensions escalated over the course of the semester, resulting in several altercations. Last December, six African American students were involved in a school fight in which a white student suffered a concussion--though he was able to attend a school function that same evening. The six were subsequently charged with attempted second-degree murder and conspiracy, charges that would have put them in jail for a combined 100 years without parole...<p>
Recently, the prosecution has announced that charges against Shaw and Jones have been reduced, and we are grateful that Mychal Bell's initial conviction was dismissed. That is precisely why now is the time to keep up the pressure and get all of these ridiculously punitive charges dismissed as well. We stand with the thousands around the country who marched on September 20 to just say no to double standards and racism in the criminal justice system.<p>

