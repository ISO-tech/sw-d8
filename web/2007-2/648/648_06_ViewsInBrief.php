<p><a name="Disease"></a><font face="Times New Roman, Times, serif" size="4"><b>A disease nurtured by ignorance</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I WAS both saddened and angered by your article ("On the march against racism," September 29). It is beyond belief that this level of unmitigated racism exists in your country.<p>
Racism is a disease nurtured by ignorance and blind hate. I even compare racism with the Dark Ages, where reason rarely saw the light of day. I wish I could have attended the September 20 protest to support the Jena 6 .<p>
I was surprised to read about racially segregated elementary schools. This is truly appalling and unacceptable. In my humble opinion, the way to fight this disease is through education, and with eloquent writers such as yourself. The fight is monumental, and I wish you every possible success.<br>
<b>Gary McLaughlin</b>, from the Internet<p>

