<p><a name="Doctors"></a><font face="Times New Roman, Times, serif" size="4"><b>Doctors standing against torture</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ONE OF the guiding principles of physicians is "first, do no harm." Yet some have actively aided the U.S. military in its physical and mental torture of detainees.<p>
Former prisoners at Guant&aacute;namo have described being systematically mentally broken by interrogators, and indeed, the U.S. military admits it has employed "behavioral science teams," including psychiatrists and psychologists, at both Guant&aacute;namo and Abu Ghraib to participate in the design of interrogation plans and then monitor their implementation.<p>
Such plans included the unethical and illegal consulting of detainees' medical records to search for the most "appropriate" way to break them psychologically--the use of dogs, for example.<p>
A growing number of doctors are speaking out against their colleagues' involvement in such practices, however.<p>
A recent letter in the British medical journal <i>The Lancet,</i> titled "Biko to Guant&aacute;namo: 30 Years of Medical Involvement in Torture," is signed by some 260 health care workers from 16 countries, including Britain, the U.S. and South Africa, and compares the ongoing role of U.S. doctors working at Guant&aacute;namo to the South African doctors involved in the case of anti-apartheid activist Steve Biko, who was murdered in 1977 while being detained by security police.<p>
Initially, the South African government blamed Biko's death on a hunger strike--but it was later revealed that he died of extensive head injuries suffered during a brutal interrogation, and that two doctors, Benjamin Tucker and Ivor Lang, had failed to provide medical care.<p>
Today, say the doctors in the <i>Lancet</i> letter, "We suspect that the doctors in Guant&aacute;namo and elsewhere have made the same mistake as Tucker who, in 1991 expressing remorse and seeking reinstatement, said: 'I had gradually lost the fearless independence...and become too closely identified with the organs of the State, especially the police force.'"<p>
The letter adds that "No health care worker in the War on Terror has been charged or convicted of any significant offence despite numerous instances documented including fraudulent record-keeping on detainees who have died as a result of failed interrogations...The attitude of the U.S. military establishment appears to be one of 'See no evil, hear no evil, speak no evil.'"<p>
Others are also speaking out. Last month, best-selling author and psychologist Mary Pipher returned her Presidential Citation award from the American Psychological Association (APA) in protest of the group's failure to pass a resolution prohibiting its members' professional involvement in CIA and military interrogations. On August 19, the APA shamefully reaffirmed a motion that allows its members to continue working on interrogation teams that are not subject to the Geneva Conventions.<p>
As Pipher wrote in a letter to the APA explaining her decision, "I do not want an award from an organization that sanctions its members' participation in the enhanced interrogations at CIA 'black sites' and at Guant&aacute;namo. The presence of psychologists has both educated the interrogation teams in more skillful methods of breaking people down and legitimized the process of torture in defiance of the Geneva Conventions.<p>
"The behavior of psychologists on these enhanced interrogation teams violates our own Code of Ethics (2002) in which we pledge to respect the dignity and worth of all people, with special responsibility towards the most vulnerable. I consider prisoners in secret CIA-run facilities with no right of habeas corpus or access to attorneys, family or media to be highly vulnerable. I also believe that when any of us are degraded, all of human life is degraded...<p>
"I know that the return of my Presidential Citation...will be of small import, but it is what I can do to disassociate myself from what I consider to be a heinous policy. All of my life, I have tried my best to stand up for those with no voices and no power. The prisoners our government labels as enemy combatants are in this category."<br>
<b>Nicole Colson</b>, Chicago<p>
