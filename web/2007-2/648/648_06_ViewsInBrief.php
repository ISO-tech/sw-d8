<a name="Dirty"></a><font face="Times New Roman, Times, serif" size="4"><b>Dirty U.S. record in Southeast Asia</b></font><p>

<font face="Times New Roman, Times, serif" size="3"><i>SOCIALIST WORKER'S </i>editorial, "Lying about Vietnam to justify his war," was right on target (August 31).<p>
It should be added that it was the U.S. that supported the Khmer Rouge regime's bid for a UN seat. This support went hand-in-hand with U.S. support of the Chinese/Pol Pot alliance against the Vietnamese Stalinists, and a growing rapprochement between the U.S. and the People's Republic of China.<p>
The Khmer Rouge tyranny was ended when the Vietnamese invaded Cambodia and installed its own, far less odious puppets. It also needs to be pointed out, in correcting Bush's revisionist history, that the Vietnamese "boat people" were largely ethnic Chinese who were the victims of a repressive campaign intensified by the U.S./Khmer Rouge/Chinese full court press on the new regime in Saigon.<p>
Finally, as you point out so well, the onus for the Cambodian killing fields rests squarely on the shoulders of U.S. imperialism and the Pol Pot regime that it enabled.<br>
<b>Bob Montgomery</b>, Boston<p>
