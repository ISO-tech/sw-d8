<font face="Times New Roman, Times, serif" size="5"><b>Watada court-martial put on hold</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 12, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>SAM BERNSTEIN reports on a setback for the Army prosecution of the first officer to resist the war.</b><p>

<font face="Times New Roman, Times, serif" size="3">TACOMA, Wash.--The U.S. Army suffered a setback in its ongoing effort to make an example out of Lt. Ehren Watada, the first officer to refuse to serve in the Iraq war.<p>
In a rare intervention of a civilian court into the military "justice" system, U.S. District Court Judge Benjamin Settle granted an emergency stay to stop Watada's court-martial proceedings.<p>
The court-martial was scheduled to resume on October 9, after a mistrial was declared in February and the case was subsequently postponed several times over the past eight months. Now the trial will be postponed for at least another three weeks.<p>
At issue is whether retrying Watada would violate the U.S. Constitution's ban on "double jeopardy"--under which a defendant can't be tried twice for the same charges.<p>
Watada made the courageous decision in June 2006 to refuse to deploy to Iraq in protest of what he considers an illegal and immoral war. The Army filed charges against Watada, including "missing movement" for declining to join his unit when it shipped out. It added "conduct unbecoming an office and a gentleman" for the "crime" of giving antiwar interviews and speeches.<p>
Watada faced six years in a military prison and a dishonorable discharge. Shortly before the February trial, though, officials agreed to drop some charges--reducing his maximum sentence to four years--when Watada signed a "stipulation" saying he did miss movement and publicly denounce the war.<p>
But at the last minute, when Watada was about to take the stand, the judge cast doubt on the stipulation, claiming that it amounted to a confession. The prosecution called for a mistrial. With the stipulation thrown out, Watada again faces up to six years in the brig.<p>
As soon as the October date was set, Watada's lawyers filed challenges in the U.S. Court of Appeals for the Armed Forces, which ruled that Watada could be court-martialed again. Then Watada's lawyers appealed to the U.S. Circuit Court for the Armed Forces, but never received a response. They then filed a request for an emergency delay in a civilian federal court.<p>
With the planned court-martial quickly approaching, antiwar activists organized rallies at Fort Lewis. At the last minute, Judge Settle ruled that the federal court had jurisdiction over this issue and ordered a delay of the trial in order to further examine the issue of double jeopardy.<p>
Watada's attorneys are also asking that he be allowed to leave the Army since his term of service ended in December, but the pending legal proceedings have prevented his discharge.<p>
In a press release the day Settle was supposed to give his ruling, Amnesty International declared that the "conviction of Watada would violate his international rights." The <i>Seattle Post-Intelligencer </i>published an editorial entitled "Watada Court-Martial: Let Him Go."<p>
Now there will be three weeks of hearings so that Settle can decide whether a retrial amounts to double jeopardy. If it doesn't, a retrial would begin as early as October 26. Antiwar activists--led by Iraq Veterans Against the War, Veterans for Peace, Military Families Speak Out and Vietnam Veterans Against the War--will use the next three weeks to organize solidarity protests.<p>
Mobilizations over the past year or so have played a crucial role in raising wider awareness of and support for Watada's case. On the first day of the original February court-martial, more than 1,000 people converged on the gates of Fort Lewis to show their support.<p>
If the judge rules that another court-martial is in fact illegal under double jeopardy, Watada would go free once and for all. Watada's stand could be a major defeat for the military. If Watada is freed, it will embolden other soldiers to follow their consciences and abandon the war on Iraq.<p>

