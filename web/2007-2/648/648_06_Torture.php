<font face="Times New Roman, Times, serif" size="5"><b>Police torture victims still waiting for justice</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 12, 2007 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">MARK CLEMENTS is a victim of torture at the hands of Chicago police and has been incarcerated since age 16--with four life sentences plus 30 years--for a crime he did not commit. He is fighting for justice for himself and other victims of police torture. His next scheduled court date is October 31 at 9 a.m., before Judge Jorge Alonzo at 2600 S. California Ave. in Chicago.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">OVER THREE decades of brutal beatings; torture with an electric homemade shockbox; attempted suffocations with a plastic typewriter cover; inserting an object in the rectum; grabbing, pulling and squeezing the penises and genitals of African American and Latino men to force them to repeat, make and sign confessions inside Area Two and Three Violent Crime Units.<p>
These humiliating and unthinkable acts all occurred to suspects interrogated by Chicago police detectives.<p>
For 30 years, African American and Latino men have described how white detectives victimized them in these two police stations, while judge after judge and prosecutor after prosecutor denied the men's claims, which has led to innocent men sitting in death row prison cells waiting on death to be administered to their bodies by the state of Illinois. This racist ring of detectives did not spare young juveniles who were very vulnerable targets.<p>
These acts of torture have led to Illinois lawmakers making it mandatory to videotape most interrogations and confessions. However, during this 30-year run of barbaric torture, no videos were made of these men's interrogations and confessions. Out of the nearly 200 men, 26 remain incarcerated.<p>
On July 19, 2006, Cook County special prosecutors assigned to investigate Chicago police torture released their long-awaited report, which described a torture epidemic at these two police stations during this era.<p>
The special prosecutors noted that criminal charges would have been filed in three of the cases because of the overwhelming medical proof, but the criminal statute of limitations expired in which to file charges. They further revealed that it was their opinion that torture occurred in many other cases as well, but there was a lack of medical proof to bring criminal charges.<p>
Nearly three months ago, the Cook County Board enacted a resolution calling on Illinois Attorney General Lisa Madigan to grant the 26 men hearings and new trials. To this day, none of the men have even been contacted by Madigan, nor have their lawyers, causing them to question her seriousness toward correcting Chicago police torture and provide them with an opportunity to seek their release from this nightmare.<p>
Stanley Wrice, an Area Two torture victim who has been incarcerated since the early 1980s. recently wrote Madigan, encouraging her to follow the resolution.<p>
If torture victims cannot get elected officials to correct the injustices that prosecutors have now said occurred, how can justice ever become reality for a Chicago police torture victim still incarcerated?<p>
Please write to Illinois Attorney General Lisa Madigan, James R. Thompson Center, 100 W. Randolph St., Chicago, IL 60601. Tell Madigan to grant hearings and new trials, and to free the 26!<br>
<b>Mark Clements</b>, Free the 26 Movement, Pontiac, Ill.<p>

