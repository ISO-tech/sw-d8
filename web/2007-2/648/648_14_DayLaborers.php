<font face="Times New Roman, Times, serif" size="5"><b>Fighting for day laborers in Virginia</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 12, 2007 | Page 14</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>ZACH MASON describes a battle against anti-immigrant scapegoating in northern Virginia.</b><p>

HERNDON, Va.--The Official Workers Center here closed September 14, the day that a conditional use permit for its building expired.<p>
The day laborer center had been highly controversial even before its opening in December 2005. The town council that approved the center had seen it as a way to appease residents who took issue with dozens of immigrant workers congregating at a local 7-11 store to wait for to be picked up by potential employers.<p>
Plans for creating a space for day laborers to gather go back to 2003, when council members tried to appease right-wingers opposed to the "eyesore" of a public gathering place for day laborers.<p>
But in the lead-up to the center's opening in 2005, the racist vigilante Minutemen, who had previously operated mainly on the U.S.-Mexico border, founded a chapter in Herndon. They began by harassing day laborers and their potential employers by photographing them.<p>
Although this campaign quickly fizzled, the Minutemen did establish a base of activists and pushed the debate in Herndon further to right.<p>
In late 2005, the town council passed an ordinance banning solicitation of work in public places. This measure coincided with the opening of the workers' center, which became the only place that workers could legally congregate.<p>
The nonprofit organization Project for Hope and Harmony, contracted by the city to operate the center, provided English classes for workers while making arrangements with contractors in need of their labor.<p>
Within months of the center's opening, the mayor and four of the six town council members were voted out of office in favor of candidates who opposed the center. The newly elected council demanded that Project for Hope and Harmony verify the immigration status of all workers using the site. When the organization refused to comply, the council announced its intention to find another way to exclude the workers.<p>
On August 28, Judge Leslie Alden of the Fairfax County Circuit Court ruled that Herndon's anti-solicitation ordinance was unconstitutional. The decision came on the heels of a March 2007 ruling that overturned anti-immigrant laws in Hazleton, Penn.<p>
Elsewhere in the state, the Prince William County Board of Supervisors passed a resolution calling for deputizing local police as immigration agents and encouraging social service providers to deny services to undocumented people.<p>
However, this met a defiant and well-organized response from the immigrant community. Immigrants and antiracists organized a boycott of all businesses in the county that did not bear a poster opposing the resolution. This slowed activity to a trickle at many businesses, such as gas stations and fast-food restaurants. The weeklong boycott culminated in a demonstration of over 10,000 in the small city of Woodbridge.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">THE REASON for this markedly different response is not mysterious. In Prince William County, a core of worker-activists from Mexicanos Sin Fronteras (MSF) and their supporters have done consistent grassroots organizing within the immigrant community.<p>
MSF was founded in Woodbridge in 2000 and now has half a dozen chapters in Maryland and Virginia. They have called for a one-day general strike of all immigrant workers and supporters in Prince William County on October 9 if the resolution is not rescinded.<p>
As MSF prepares for a potential strike, lawyers are challenging the resolution in court. But whatever the judge decides, local immigrant communities have proven to themselves and others that organizing--and not waiting for the salvation of courts--is the best way forward.<p>
Back in nearby Fairfax County, where the workers' center is now closed, immigrant workers now meet in a small park, signing up each morning on a list for those seeking employment that day. But the future is far from certain, and the atmosphere remains tense as the anti-immigrant mayor and town council remain in place.<p>
While the future of the movement may not be certain, the need to carry out a long-term ideological battle against anti-immigrant bigotry is clear.<p>
The reason why some cities declare sanctuary for undocumented immigrants while other local governments declare virtual war on immigrant communities is that a battle has been fought and won by either the forces of progress or the forces of reaction.<p>

