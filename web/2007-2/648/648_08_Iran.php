<font face="Times New Roman, Times, serif" size="5"><b>Using women's rights as an excuse for war</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">October 12, 2007 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>DEEPA KUMAR examines the U.S. propaganda campaign against Iran.</b><p>

AT LEAST since the Spanish-American war of 1898, warmongers in the U.S. have found it useful to use a humanitarian cover as justification for war.<p>
Just as the yellow press howled about the cruel Spaniards torturing innocent Cubans more than a century ago, today's corporate media are awash with stories that express moral indignation about the "evil" Mahmoud Ahmadinejad and his repression of Iranian people.<p>
What is conveniently absent in these "rescue" stories is that ordinary people have the ability to stand up for themselves.<p>
To be sure, Ahmadinejad's government has brutally cracked down on protest, and harassed and arrested human rights and women's rights activists. Ahmadinejad also holds reactionary views about the Nazi Holocaust of Jews, homosexuality and any number of other issues.<p>
However, what is missing and often downplayed in the mainstream "white man's burden" story is that Iranians have the ability and the will to fight back. In Iran today, several vibrant human rights movements have voiced opposition to the Ahmadinejad government, and it is they who should decide the future of their country. They do not need the "help" of the U.S. government.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">IRAN CURRENTLY follows a conservative interpretation of Islamic Sharia Law that systematically discriminates against women. For instance, a woman's testimony in court is worth half that of a man. The laws also deny women equal rights in divorce, custody and inheritance.<p>
United by a call for equality before the law, women's groups, including both secular and Islamic feminists, called for a demonstration in June 2006. Thousands of women participated in this demonstration, but the police violently cracked down on them and arrested dozens.<p>
Subsequently, this re-energized women's movement launched a campaign to collect 1 million signatures demanding equality for women.<p>
The campaign is also designed as an exercise in consciousness-raising. Many members of the movement have stated that simply seeking legal change is not sufficient, and that what is necessary is social mobilization. To achieve this, the campaign has trained hundreds of women to educate others about injustices, but it also aims to learn from ordinary women about their needs and demands.<p>
The campaign has presented itself as not being at odds with Sharia law, but simply offering alternative interpretations. As of August 2007, 100,000 people had signed on.<p>
There are contradictions and tensions within the movement itself. Religious and secular feminist tendencies in Iran have often been at odds with each other.<p>
When the U.S.-backed Shah was deposed by mass demonstrations and strikes that expressed discontent at his repressive and corrupt rule in 1979, both religious and secular women played a part. The Islamist forces mobilized religious women based on strong role models drawn from Islam's history. Secular women, too, started to wear the hijab as a symbol of protest against the Westernized Shah.<p>
Once Ayatollah Khomeini secured the reigns of power, however, he introduced forced veiling. This prompted massive protests such as the one on International Women's Day in March 1979. However, there were also large numbers of counter-protesters, including religious women, at this event.<p>
Since the early 1980s, a trend called Islamic Feminism has come into being, whose proponents use arguments taken from Islamic<i> </i>texts and traditions rather than from Western thinkers to make an argument in support of women's rights.<p>
Today, activist efforts of both religious and secular women have meant that women hold a variety of jobs--they are doctors, lawyers, teachers, etc. Unlike in Saudi Arabia, the U.S. government's long-term ally, where women are forbidden from driving cars, Iranian women have long done so.<p>
Women comprise 65 percent of university students, making them more educated than their male counterparts and putting them on par with women in advanced capitalist nations. The female literacy rate stands at 80 percent. There are many women's rights groups, several feminist magazines and many women's magazines.<p>
In addition to fighting legal battles, women and their male allies have participated in the electoral process, gradually increasing the number of women in government.<p>
Women voted in large numbers for the reformist President Mohammad Khatami in 1997 and 2001. But while Khatami relaxed cultural restrictions and censorship, he did not do much by way of reform. This along with other betrayals led to the defeat of the reformists in 2005 elections, and the emergence of Ahmadinejad.<p>
The women's moment in Iran today is stronger than in many parts of the Middle East. While the movement faces challenges both within (secular vs. Islamic feminism) and without (the brutality of the current administration), it will not be helped by U.S. bombs. As Shirin Ebadi, a prominent lawyer and human rights activist in Iran and a recipient of the Nobel Peace prize put it:<p>

<blockquote>
American policy toward the Middle East, and Iran in particular, is often couched in the language of promoting human rights. No one would deny the importance of that goal. But for human rights defenders in Iran, the possibility of a foreign military attack on their country represents an utter disaster for their cause.
</blockquote><p>

The U.S. did not "liberate" Afghan women, and it will not liberate Iranian women either. If anything, the women's movement in Iran should serve as an inspiration for the kind of grassroots activism so badly needed in U.S., where abortion rights and other rights for women have been whittled down over the last few decades.<p>

