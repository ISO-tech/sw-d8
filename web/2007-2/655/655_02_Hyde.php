<font face="Times New Roman, Times, serif" size="5"><b>The real Mr. Hyde's hateful record</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 7, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE GOOD news is that Henry Hyde is no longer with us. The bad news is that he left behind a legacy.<p>
With 32 years in the House of Representatives, the anti-woman archconservative was in the middle of most important Republican battles of the recent past.<p>
In 1982, he led the opposition against a nuclear freeze amendment. In 1987, he was a vocal defender of the Reagan administration and its stooge Oliver North when the Iran-contra scandal was exposed.<p>
In 1998, Hyde was part of the drive to impeach Bill Clinton based on lying about an affair with a White House intern--even as <i>Salon </i>magazine uncovered Hyde's own extramarital affair from the 1960s.<p>
Yes, Hyde had a long and memorable career. But it was one of his first acts on taking a seat in the House that had the greatest impact, especially on the lives of millions of women--the Hyde Amendment.<p>
With its <i>Roe v. Wade </i>ruling in 1973, the Supreme Court made abortion legal, putting an end to decades of women dying from illegal, back-alley abortions. The decision came in the context of mobilization and protest, and a sea change in public attitudes about women's position in society.<p>
Hyde understood that it would be impossible to overturn <i>Roe </i>all at once. So he dealt the first blow in what would become a decades-long crusade to restrict women's access to abortion, one piece at a time.<p>
In 1976, he proposed an amendment to an appropriations bill to ban the use of federal funds for women seeking abortions. At that time, about a third of women seeking legal abortions (often young girls) got help from Medicaid.<p>
In less than a month, the Hyde Amendment claimed its first victim: Rosie Jimenez, a Medicaid recipient and mother of two in Texas, who bled to death after obtaining the only abortion she could afford--a dangerous, illegal one.<p>
The Hyde Amendment has passed a vote in Congress every time it's come up since, no matter which party was in the majority. When Democratic President Jimmy Carter was asked whether the Hyde Amendment was unfair to poor women, he responded, "As you know there are many things in life that are not fair, that wealthy people can afford and poor people can't."<p>
Life has indeed been "unfair" to the millions of women who are too poor to afford an abortion--unfair and deadly.<p>
Up to the end of his miserable life, Hyde and his fellow anti-abortion fanatics kept up their assault on women's right to choose, passing bans on late-term abortion procedures and restrictions like mandatory waiting periods and parental consent laws.<p>
Hyde also lent his support to anti-abortion shock troops in the streets--for example, when he testified on behalf of the monstrous Joe Scheidler and his Pro-Life Action League's campaign to harass women as they enter clinics.<p>
Last week, conservatives stepped up to eulogize their dead hero. "This fine man believed in the power of freedom, and he was a tireless champion of the weak and forgotten," Bush said. "He used his talents to build a more hopeful America and promote a culture of life."<p>
Where was the eulogy for Rosie Jimenez and all the other nameless women who suffered from--and sometimes lost their lives to--Hyde's anti-women, anti-choice policies?<p>

