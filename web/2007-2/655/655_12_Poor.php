<font face="Times New Roman, Times, serif" size="5"><b>When the Constitution doesn't apply</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 7, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">IF YOU'RE poor, the Fourth Amendment doesn't apply to you.<p>
This was the message from the U.S. Supreme Court with its November 26 decision not to hear arguments on whether San Diego law enforcement should be able to continue searches of welfare recipients' homes without a warrant.<p>
In 1997, San Diego County instituted "Project 100 Percent," in which applicants for welfare must agree to a "walk-though" of their homes to look for supposed evidence of fraud. Inspectors from the county district attorney's office are allowed to enter homes unannounced to check on several things: whether the person has the assets they claimed, whether a child they claimed is living in the home, whether a spouse reported "absent" doesn't live there.<p>
No evidence is required for a search to take place, and a person who refuses to submit could lose their benefits.<p>
But under the Fourth Amendment of the U.S. Constitution, police are supposed to obtain a warrant before they are allowed to search a person's home. The ACLU filed a lawsuit arguing as much on behalf of six single parents who were seeking assistance in San Diego County.<p>
Yet despite this obvious violation of the ban on unreasonable search and seizure, a federal judge ruled for the county, and a three-judge panel of the 9th U.S. Circuit Court of Appeals affirmed that ruling in a 2-1 decision last year.<p>
The 9th Circuit ruled that the searches were for the purpose of deciding eligibility, not a criminal investigation, and therefore didn't violate the Fourth Amendment. The ruling cited a 1971 court challenge, <i>Wyman v. James, </i>in which the searches were performed by social workers, not law enforcement.<p>
But as dissenting justice Raymond Fisher pointed out in his opinion, "Project 100 Percent home visits entail a law enforcement agent--trained not to give advice to welfare applicants--walking through the applicant's home in search of physical evidence of ineligibility that could lead to criminal prosecution, either for welfare fraud or other crimes unrelated to the welfare application."<p>
He also wrote, "<i>Wyman </i>does not support the majority's unprecedented conclusion that no search occurs under the Fourth Amendment when a district attorney fraud investigator roams through a welfare applicant's home, scrutinizing the most intimate and private of places, looking for evidence of ineligibility, fraud and crimes wholly unrelated to the welfare application."<p>
The 9th Circuit court's ruling is smoke and mirrors to protect a policy that sweeps away the civil liberties of a whole group of people--for no other reason than that they are poor. The justices also ruled that since the searches were "voluntary," they didn't violate any law. But the consequences of refusing to be searched could be a matter of life and death to poor families who depend on benefits.<p>
"The government doesn't search through the closets and medicine cabinets of farmers receiving subsidies," wrote Judge Harry Pregerson, one of seven judges who filed a dissent in April arguing that the full 9th Circuit should reconsider the panel's ruling.<p>
"They do not dig through the laundry baskets and garbage pails of real-estate developers or radio broadcasters." Only the poor, he said, must "give up their rights of privacy in exchange for essential public assistance."<p>
When the ACLU requested that the Supreme Court examine the case, it refused, without comment.<p>
If Project 100 Percent is allowed to stand, it will pave the way for other punitive regulations aimed at welfare recipients. As state governments try to meet stringent rules tied to the federal funding they receive, like requirements that recipients work, poor people will pay the price.<p>

