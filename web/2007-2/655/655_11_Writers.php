<font face="Times New Roman, Times, serif" size="3">"For made-for-Internet material, they offered minimums that would allow a studio to produce up to a 15-minute episode of network-derived Web content for a script fee of $1,300. They continued to refuse to grant jurisdiction over original content for the Internet."<p>
In other words, even though more and more television reruns and films are viewed over the Internet, the AMPTP wants to pay writers a fraction of what they would get if their show or film was rebroadcast on television--a small, one-time flat fee, regardless of how many times it is viewed, downloaded and so on.<p>
In addition, the media moguls refuse to discuss the residual formula for downloads and want to keep the right to call any reused material "promotional"--including full-length movies--and pay writers nothing at all.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">WHILE THE AMPTP is often referred to benignly as "the producers," the fact is that the WGA is up against the biggest media conglomerates in the world: Time Warner (which owns Warner Brothers and part of the CW network), Viacom (which owns Paramount and the other part of the CW network), News Corporation (Fox), CBS Corp., Walt Disney Co. (ABC), and General Electric (NBC and Universal).<p>
It's clear that these are already raking in the big bucks from the Internet. The <i>Financial Times </i>reported that ABC, CBS, NBC and Fox alone are expected to take in $120 million in 2007 from Web ads. "The total online video advertising market will be worth close to $1.3 billion this year after doubling in size in 2006," the <i>Financial Times </i>reported, citing a digital media research company.<p>
To their stockholders, Rupert Murdoch (Fox) and Bob Iger (Disney/ABC) brag about the millions they'll bring in through online programming. But to the WGA, they claim ignorance of this "new media."<p>
"It would cost very little money to end the strike," Judd Apatow, the screenwriter and film director, told a reporter. "[The producers] are basically trying to create a way of paying people so that when the Internet explodes, they'll wind up paying less than they do now to writers.<p>
"And I don't think they're going to get away with it. The writers really failed to stand up for themselves with the DVD [in a previous contract dispute], and they feel terrible about it, and enough of them will not give up that it will have to be resolved in a reasonably fair manner."<p>
To judge from opinion polls, the public isn't buying the line from the media corporations. According to a SurveyUSA poll released November 14, 63 percent side with the writers. Even the Democratic Party presidential candidates can see which way the wind is blowing--their wariness at being seen to cross WGA picket lines forced CBS to cancel a planned December 10 debate.<p>
The mainstream press has been playing up the impact on other industry employees and the Los Angeles economy. But a November 20 solidarity rally of 4,000 people represented dozens of LA unions, both inside and outside of the industry.<p>
Led by Teamster trucks, the participating unions included the Screen Actors Guild, Directors Guild, Teamsters, AFTRA and IATSE, Studio Utility Employees Laborers Local 724, Professional Musicians Local 47, California Nurses Association and many more.<p>

