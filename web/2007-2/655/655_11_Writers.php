<font face="Times New Roman, Times, serif" size="5"><b>The TV bosses' insulting offer</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 7, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>CINDY KAFFEN reports that striking television and film writers are standing firm on their demand for fairness.</b><p>

A STRIKE by Writers Guild of America (WGA) members is beginning its second month with no end in sight, despite claims by television and film producers that they are offering a fair deal.<p>
The most important issue in the strike is whether writers will get paid when their work--whether made for television, theater showings or downloading only--is provided to consumers via the Internet. Production companies claim they can't estimate how much revenue the Internet will generate, so they want writers to take a nominal fee.<p>
Four days of negotiations last week between the WGA and the Alliance of Motion Picture and Television Producers (AMPTP)--the first since the strike began--left writers fuming. In a statement to members, the union's negotiating committee said the AMPTP's offer, which it calls a "New Economic Partnership," in reality "amounts to a massive rollback."<p>
"For streaming television episodes," the union negotiators said, "the companies proposed a residual structure of a single fixed payment of less than $250 for a year's reuse of an hour-long program (compared to over $20,000 payable for a network rerun). For theatrical product, they are offering no residuals whatsoever for streaming.<p>

