<p><a name="Baltimore"></a><font face="Times New Roman, Times, serif" size="4"><b>Baltimore hotel workers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Dan Sparks</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BALTIMORE--Hotel workers at the Sheraton City Center have taken their struggle for a fair contract to the streets. After more than 18 months without a contract, they held a rally and march November 15 that drew more than 200 people.<p>
William Yung III, CEO of the Columbia Sussex hotel empire, which owns the City Center, has made a point of running the most efficient company in the business. This has made stockholders happy--but not hotel workers, who continue to work long hours in terrible conditions.<p>
Columbia Sussex is refusing to budge from a contract proposal that would increase the cost of health care, add to the workload of employees and raise wages by less than the rate of inflation.<p>
But support for the Sheraton City Center workers, organized by UNITE HERE Local 7, was on display at rally. UNITE HERE delegations from Washington, D.C., Philadelphia and Atlantic City attended, and actor and activist Danny Glover was among the featured speakers. After the speeches, the rally turned into a moving picket outside the front entrance of the hotel.<p>
Workers have voted to call for a boycott of the hotel, which they are supporting with regular leafleting and plans for more rallies to keep up the pressure.<p>

