<a name="King"></a><font face="Times New Roman, Times, serif" size="4"><b>Farmworkers and Burger King</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><p>

<font face="Times New Roman, Times, serif" size="3">MIAMI--More than 1,500 tomato pickers from the Coalition of Immokalee Workers (CIW) and their supporters made the nine-mile march to Burger King headquarters November 30 to demand fair conditions and wages in the fields.<p>
The CIW, a community-based organization founded six years ago in southern Florida, wants Burger King to honor an agreement like the one fast-food giants McDonald's and Yum! Brands (Taco Bell, KFC, Pizza Hut, Long John Silver's and A&amp;W Restaurants) have already agreed to.<p>
In 2005, the CIW won a four-year campaign that forced Taco Bell to agree to pay a penny more per pound for tomatoes, with the stipulation that the entire amount be passed on to pickers. Last April, McDonald's agreed to a similar arrangement, increasing the wages of pickers to about 77 cents per bucket.<p>
According to the CIW's Campaign for Fair Food, tomato pickers are paid virtually the same per-bucket piece rate (roughly 45 cents for a 32-pound bucket) today as they were in 1980. They have no right to overtime pay, even though many work 60- to 70-hour weeks, and no collective bargaining rights. Workers regularly face the threat of deportation and are regular targets of abuse by employers.<p>
But Burger King refuses to let anything get in the way of its profits. Instead of adhering to a code of conduct agreed upon by Taco Bell and McDonald's, Burger King heads say they will follow a business-friendly monitoring program called Socially Accountable Farm Employers, which is employer-controlled.<p>
When marchers arrived at corporate headquarters on Friday, they brought with them a trailerload of work boots that had been collected from farmworkers with the message, "Doubt our poverty: walk in our shoes." Workers were responding to claims by Burger King CEO John Chidsey, during an October 3 speech to his alma mater Davidson College, that farmworker poverty was a "myth."<p>
Burger King's greedy intransigence is backed up by a recent move by the Florida Tomato Growers Exchange, which has threatened a $100,000 fine for any grower who accepts an extra penny per pound for pickers' wages.<p>
<i>For information on how to support the struggle to make the King pay a living wage, see <a href="http://www.ciw-online.org" target="_blank">www.ciw-online.org</a></i>.<p>
