<font face="Times New Roman, Times, serif" size="5"><b>This tragedy didn't have to happen</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 7, 2007 | Page 5</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>PATRICK DYER reports on a deadly fire in an Ohio home where the utilities were cut off.</b><p>

A FIRE blazed through a Toledo, Ohio, home November 28, killing 29-year-old Michele Crawford and her three children, 11-year-old Yaniela, 7-year-old Victoria and 4-year-old Ivan. The only survivor was Anthony Diaz, Michele's fianc&eacute;e and Ivan's father.<p>
The family had moved to the house so they could be closer to a hospital where Ivan had undergone chemotherapy and radiation treatment for kidney cancer. Fire Department officials reported that the lethal fire was started by a candle in the unheated house.<p>
Why were candles burning in a cold house with children in the winter? Because Toledo Edison, a subsidiary of FirstEnergy Corp., turned off the family's electricity that day for not paying a $98 deposit to set up a new account.<p>
While community members were generously donating to a fund to help with funeral costs for the family, collecting $4,500 within the first couple days, FirstEnergy spent time after the deadly fire deflecting responsibility.<p>
In an interview with the <i>Toledo Blade</i>, a FirstEnergy spokesperson blamed the victims, saying that Crawford and Diaz had called several times but never followed through. "Actually, there would have been time to get [power] turned back on that same day if somebody had taken action," the spokesperson declared. "But we didn't hear a word, and now we have this very tragic situation."<p>
This callous disregard for the lives of ordinary people is how business is done at FirstEnergy Corp., which raked in more than $1 billion in profits last year.<p>
One byproduct of its greed was the dumping of toxic chemicals into our air--21 million pounds of it a year, including sulfuric acid, hydrochloric acid and arsenic, according to a study by researchers from the University of Massachusetts Amherst.<p>
FirstEnergy's other crimes include some measure of responsibility for the 2003 Northeast blackout, the largest in North American history, as well as covering up "serious safety violations" that caused a major leak at the Davis-Besse nuclear power plant outside Toledo--the result, the company later admitted in court, of putting "production over safety."<p>
FirstEnergy CEO Anthony Alexander took home a whopping $12.8 million in salary and bonuses last year, plus another $11.5 million worth of stock options.<p>
And while Alexander may not be so generous with poor customers, he is with his corporate and political accomplices. He was a "Bush Pioneer" in both 2000 and 2004, donating more than $100,000 to the Republican presidential campaign--plus he gave another $100,000 for Bush/Cheney inauguration merrymaking.<p>
Just two weeks before FirstEnergy cut off electricity for Michele Crawford and her family, Alexander testified before the Ohio House of Representatives in support of continued industry deregulation, saying that "customers should not be deprived of the opportunities presented by a competitive market."<p>
For the Crawford family, these "opportunities" cost them their lives.<p>

