<font face="Times New Roman, Times, serif" size="5"><b>A history of rebellion</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Amy Muldoon</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 7, 2007 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i><a href="http://www.cultureproject.org/index.php?option=com_content&amp;task=view&amp;id=61" target="_blank">Rebel Voices</a>, </b></i>written by Rob Urbinati, with Howard Zinn and Anthony Arnove; directed by Will Pomerantz and Urbinati; cast includes Staceyann Chinn and Allison Moorer. At the Culture Project in New York until December 16.</font><p>

<font face="Times New Roman, Times, serif" size="3">MANY RADICALS credit Howard Zinn's <i>A People's History of the United States </i>as one, if not the, book that opened their eyes to the rich tradition of progressive grassroots struggle in this country.<p>
In 2004, Zinn and co-editor Anthony Arnove released <i>Voices of a People's History of the United States, </i>a companion edition that collects thousands of primary documents that give life to the story of <i>A People's History </i>through the speeches, articles, songs, posters and writings of the men and women who shaped the movements.<p>
Now the legacy continues with <i>Rebel Voices, </i>written by Rob Urbinati and directed by Urbinati and Will Pomerantz. The new stage play moves chronologically through U.S. history with readings from famous and unknown organizers, and is framed with songs that express the same spirit of rebellion, opening with Patti Smith's "People Have the Power" sung by Allison Moorer.<p>
Zinn himself is the first reading, and sets the tone that this is not an objective look at U.S. history, but a very biased one: "The history of any country conceals fierce conflicts of interest--sometimes exploding, most often repressed--between conquerors and conquered, masters and slaves, dominators and dominated in race and sex. And in such a world of conflict, a world of victims and executioners, it is the job of thinking people not to be on the side of the executioners."<p>
What follows is a who's who of resistance, from the well-known favorites like Malcolm X, to modern fighters like Katrina survivor Patricia Thompson, war resister Camilo Mej&iacute;a and Gold Star Families for Peace founder Cindy Sheehan. Urbanati's choice of texts is satisfying as individual pieces, but also because of their interaction.<p>
The U.S. Strategic Bombing Survey Summary Report that describes the effects of the atom bomb dropped on Hiroshima is a tally of disinterested scientific data, but chaffing up against Yamaoka Michiko's personal account of the carnage, it becomes the stuff of nightmares.<p>
One of the show's highlights is three contemporary working-class women's accounts of how they became activists in the Depression-era wave of struggle. Stella Nowicki, Rose Chernin and Genora Dollinger (performed by Morgan Hallett, Opal Alladin and Lenelle Moise) tell their tales of glory, overlapping and weaving in and out of each other's monologues.<p>
The effect elevates their inspirational stories and gives a picture of a generation of women fighters emboldened by their own daring and quick thinking. And of course the favorites are all there: Frederick Douglass's fiery "What to the Slave is the Fourth of July?" speech and Susan B. Anthony's acid arguments against her sentence for the "crime" of voting.<p>
The tone of the readings is not always fever pitch: the statement of Orlando and Phyllis Rodriguez, whose son died on September 11, is quietly defiant, and Cindy Sheehan piece reminds the audience that people find the strength for dissent even in moments of greatest pain.<p>
Minimally staged, the entire cast, which takes on multiple voices through the show, remains visible during the production. The stage is muted, with small risers that allow the cast members to move and interact, and black and white projections depict the authors or images from the historical moment. But the main attraction is clearly the words and voices of the players, inciting, defying, testifying and inspiring.<p>
Like many threads, the voices join together in the final piece, Douglass' famous passage on the need for struggle, culminating in his declaration "Power concedes nothing without a demand. It never did and it never will." <i>Rebel Voices </i>is a rousing reminder of the generations of women and men who have pioneered the path of resistance, and fuel for the current generation to keep the fight alive.<p>

