<font face="Times New Roman, Times, serif" size="5"><b>"Justice doesn't close at 5"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 7, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>LAURA BRADY reports on protests against a Texas judge who refused to stop an execution in order to close her office on time.</b><p>

ANTI-DEATH penalty activists delivered their message to Texas Judge Sharon Keller at a November 16 protest in Austin: Justice doesn't close at 5.<p>
Keller is responsible for the only execution that has taken place anywhere in the U.S. since the U.S. Supreme Court announced in late September that it would hear an appeal on whether lethal injection violates the Constitution's ban on cruel and unusual punishment. That decision has led to a de facto moratorium on executions until the justices make their decision, expected no sooner than next spring.<p>
Texas death row prisoner Michael Richard was scheduled to be executed on the day that the Supreme Court made its announcement. Richard's attorneys spent the afternoon compiling a 180-page application for a stay of execution until the justices reviewed the lethal injection procedure.<p>
After encountering computer problems, the lawyers contacted Keller and told her they wouldn't be able to file the appeal until 5:20 p.m. and asked her to keep the court's office open. Keller's cold-hearted response: "We close at 5." Michael's execution went ahead a few hours later.<p>
On November 16, demonstrators gathered at the Texas Court of Criminal Appeals building to call for Keller's immediate resignation. The protesters filed into the clerk's office 10 minutes before 5 p.m. to submit copies of judicial complaints bearing more than 1,600 signatures and personally written letters demanding justice.<p>
At 5:03 p.m., with several people still waiting in line to turn in their complaints, the clerk stopped accepting submissions, turned off the lights and shut the door.<p>
Campaign to End the Death Penalty member Lily Hughes read her letter aloud to the employees still inside the locked office, stating that Keller had "stepped far outside understood standards of judicial conduct, much less human decency."</FONT><FONT FACE = "Times" COLOR = "#000000" STYLE = "font-size:13pt"> </FONT><FONT FACE = "Times" COLOR = "#000000" STYLE = "font-size:12pt">Requests to keep the clerk's office open long enough to receive every letter were denied, of course--the staff had already broken the "Keller Rule" by remaining open three minutes past 5.<p>
Public outcry against Keller's actions has led to the appeals court creating an "e-mail filing system for urgent pleadings," but for Michael Richard and his family, that's too little, too late. Outside the courthouse, Michael's sister, Patricia Miller, told the crowd of activists and media, "If it weren't for Sharon Keller, he'd be here today." She demanded that Keller resign her position as judge.<p>
"Killer" Keller, as she is known to activists, is notorious for not caring about the rights of defendants. She has voted to ignore DNA evidence of innocence and ruled that a defendant in a capital murder case whose lawyer slept through parts of the proceedings didn't deserve a new trial.<p>
Michael Richard should never have been eligible for capital punishment--with an IQ of 64, his execution should have been stopped under a 2002 Supreme Court decision that bars use of the death penalty on the mentally retarded.<p>
As Richard's brother-in-law, Paul Miller, told reporters, "We know what Michael did was wrong, but also, on the other hand, this system did not treat him fair either."<p>

