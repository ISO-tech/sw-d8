<font face="Times New Roman, Times, serif" size="5"><b>Rob Will</b></font><p>

<font face="Times New Roman, Times, serif" size="2"><i>Texas death row prisoner and member of the <a href="http://drivemovement.org/" target="_blank">DRIVE (Death Row Inner-communalist Vanguard Engagement) movement</a>, which engages in nonviolent, direct action protest to fight for better conditions on death row and in opposition to capital punishment. Speaking for DRIVE, Rob sent this statement of solidarity to the Socialism conference. It was read by YUSEF SALAAM, who was wrongly convicted and imprisoned for seven years in the Central Park jogger case.</i></font><p>

<font face="Times New Roman, Times, serif" size="3">WE REVOLUTIONARIES fight because we feel that inaction in the face of injustice is nothing less than consent.<p>
We fight here on Texas death row because we not only understand that capital punishment is a race and class issue that is utilized by the state as a tool of social control, but because we also understand that all struggle is interconnected. The death penalty is a working-class issue, an injustice that should be fought against, just as we fight against the brutal occupation of Iraq, for immigrant rights and in opposition to all forms of oppression.<p>

