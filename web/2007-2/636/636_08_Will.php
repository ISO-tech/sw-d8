<font face="Times New Roman, Times, serif" size="3">Sadly enough, most anti-death penalty organizations have refused to support our actions on the inside, which they consider "radical." They feel we should "follow the rules" and abandon all forms of civil disobedience.<p>
Our comrades with the ISO have shown their active support of DRIVE's ongoing protest, which involves complete disregard and unrepentant breaking of the precious rules of this deplorable system.<p>
The ISO and our Austin comrades have shown that they deeply feel the injustice that is the death penalty through the relentless support they have shown us. (By the way, much love to Paul D'Amato for his <i>Meaning of Marxism</i> and everyone at <i>Socialist Worker </i>and <i>ISR</i>, which have helped me define my personal political outlook more clearly).<p>
In turn, DRIVE extends to the ISO and everyone else at the Socialism 2007 conference our unwavering solidarity. Look into the eyes of the person next to you. They are your comrades, our comrades and the future we are fighting to build.<p>
And in the spirit of this fight, the struggle to liberate all oppressed peoples of the world, we send each and every one of you an embrace of solidarity and resistance!<p>

