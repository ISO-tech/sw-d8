<p><a name="Bush"></a><font face="Times New Roman, Times, serif" size="4"><b>Protesting George Bush</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Nate Moore</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW LONDON, Conn.--Approximately 1,000 people turned out May 23 to protest George Bush's commencement speech at the U.S. Coast Guard Academy here.<p>
The ANSWER Coalition, Connecticut Coalition for Peace and Connecticut Families for Peace were among those who organized. The multiracial, antiwar, anti-Bush crowd consisted of socialists, anarchists, Green Party activists, Iraq veterans and the family members of veterans, among many others.<p>
Also present at the demonstration was Carlos Arredondo, whose son Alexander died in Iraq on August 24, 2004. Carlos was so distressed when he received news of his son's death that he lit the military representatives' truck on fire, badly burning himself. At the protest, he hitched a mobile memorial to his truck, in the form of a coffin in memory of Alexander.<p>
Bush's visit apparently was deemed so "important" that three area hospitals stopped accepting new patients for the duration of his stay.<p>

