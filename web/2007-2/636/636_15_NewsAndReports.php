<a name="Social"></a><font face="Times New Roman, Times, serif" size="4"><b>U.S. Social Forum</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ATLANTA--Thousands of people are expected to attend the first U.S. Social Forum (USSF) here from June 27 to July 1. The event is designed to link U.S. activists with the World Social Forum movement that began in Porto Alegre, Brazil in 2001, and that has since expanded to regional, national and local social forums around the world.<p>
The USSF began to take shape in 2003 when delegates from the U.S. to the Porto Alegre events formed a coalition, Grassroots Global Justice (GGJ), to launch an event in the U.S.<p>
The planners decided to go slow in order to help maximize participation from groups with constituencies among workers, the poor, and people of color. Plans to hold the event in the summer of 2006 were shelved after the impact of Hurricane Katrina took precedent for activists in the South.<p>
Momentum for the USSF increased as 2007 began. As Ruben Solis of the Southwest Workers' Union put it at a USSF national planning meeting in March, the event is designed not only to bring organizations together for an interchange of ideas, but also to rebuild the U.S. left.<p>
The USSF will begin with a march through Atlanta to commemorate struggles of the past--primarily the civil rights movement. The following three days will be devoted to workshops based on the themes of opposition to militarism and imperialism, fighting racial and sexual oppression, workers' rights, immigrant rights, building alternative economic models and more. A general opening session will precede the workshops, while two consecutive plenary sessions will close each day.<p>
The USSF will conclude with an assembly of social movements July 1. While the USSF itself doesn't take political positions, organizations represented in the assembly will attempt to draft a political statement and discuss the possibility of future organizing.<p>
