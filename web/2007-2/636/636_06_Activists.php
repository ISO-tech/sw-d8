<font face="Times New Roman, Times, serif" size="5"><b>Labeling activists as "terrorists"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 22, 2007 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">ON MAY 21, a federal judge in Eugene, Ore., ruled that the label of "terrorism" could be applied to some or all of the crimes committed by a group of 10 radical environmentalists. Under a 2001 Oregon law, if a crime is labeled as "terrorist," the sentence can be "enhanced" by adding up to 20 years to it.<p>
Judge Ann Aiken, a Clinton appointee, made the ruling, even though no one was injured in any of these crimes. The environmentalists committed at least 20 arsons or attempted arsons between 1995 and 2001, doing more than $40 million in property damage. Among their targets were an SUV dealership, a ski resort in Colorado, forest ranger stations, a tree farm and a Eugene police substation.<p>
The activists were rounded up as part of an FBI operation known as "Operation Backfire" and have all pled guilty to conspiracy to commit arson.<p>
So far, three of the activists have been sentenced. Stanislas Meyerhoff was sentenced to 13 years, Kevin Tubbs was sentenced to 12 years and Chelsea Dawn Gerlach was sentenced to 9 years. The minimum mandatory sentence for arson conspiracy is five years.<p>
These people were given harsh sentences, even though they co-operated with prosecutors. Because the judge ruled that their crimes were "terrorist," they will serve their sentences in high-security prisons. Their defense lawyers expressed concern that they may be the targets of violence from their fellow inmates, who will regard them as "snitches" for informing on their friends.<p>
Meyerhoff's defense lawyer argued: "Individuals who resorted to arson in a vain and misguided attempt to save the environment are not, by that motive alone, rendered vastly more dangerous than individuals who engage in a series of arson for motives such as profit or revenge."<p>
According to the 2001 law, a crime may be considered to be "terrorist" if it was meant to "intimidate" or "coerce" the government. This broad definition has ominous implications. Before the ruling, the Eugene <i>Register-Guard</i> observed that if these crimes were labeled terrorist, "other activists might stand warned that even their least destructive 'direct actions' could be prosecuted in federal court as terrorism, with a possible 20-year prison term."<p>
Lauren Regan, a lawyer and director of the Civil Liberties Defense Center in Eugene, told the <i>Register-Guard</i>, "This enhancement could certainly be applied to any movement, although historically it hasn't been applied to the right wing. A lot of antiwar and anti-nuclear activists would certainly be falling within those standards. It could easily be applied to them."<p>
So it's conceivable that antiwar activists who occupy a congressman's office could be accused of "terrorism."<p>
This ruling by Judge Aiken (did I mention that she is a Clinton appointee?) takes our country one step closer to becoming a police state.<br>
<b>Evan Kornfeld</b>, Eugene, Ore.<p>

