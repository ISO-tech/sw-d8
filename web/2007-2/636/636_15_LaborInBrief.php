<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">June 22, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>UFCW forum on falling living standards</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Steve Leigh and Brook Winter</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--United Food and Commercial Workers (UFCW) and several other unions and community groups held a public forum asking "Is Puget Sound Losing its Middle Class?" on June 10. The answer to the question from the crowd of 800 that turned out was a rousing "Yes!"<p>
UFCW currently represents an estimated 20,000 grocery workers in the Seattle area in their contract fight with the grocery giants, Safeway, Albertsons, Fred Meyer and QFC. Speakers included Pramila Jayapal of the immigrant rights organization Hate Free Zone; John de Graf, the national coordinator of Take Back Your Time; and Kristin Rowe-Finkbeiner of Moms Rising.<p>
Jayapal talked about the disproportionate effect of declining living standards on immigrants and people of color. Rowe-Finkbeiner discussed the lack of "family friendly" social policies in the U.S. like parental leave, national health care and public child care--and discussed how, for many workers, "having a baby is the leading cause of a poverty spell."<p>
According to de Graf, in the past eight years more Americans "have declared bankruptcy than have graduated from college." The U.S., he said, ranks last or nearly last in several important social measures, including equality of wealth, imprisonment rates, murder rates and child poverty.<p>
A short film was also shown that included the stories of workers from the Puget Sound area, including Sharon Russell who was diagnosed with breast cancer, but couldn't afford to call in sick due to the expense of medical treatments.<p>
Besides wages and health care, two of the key issues facing grocery workers are job scheduling and better sick leave. Right now, workers are only allowed to use sick leave on the third day of being sick, and receive their weekly schedules just three days in advance.<p>
Steve Williamson of UFCW called on the crowd to support the union in its fight for a better contract.<p>


