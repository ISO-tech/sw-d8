<font face="Times New Roman, Times, serif" size="5"><b>Political prisoner in U.S.-allied Pakistan</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 22, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">IN THEIR latest wave of political repression, authorities in Pakistan have targeted Farooq Tariq, general secretary of the Labour Party of Pakistan and a prominent activist in the struggle for democracy against the country's military government.<p>
After being arrested without a warrant June 5, he was sent to the notorious Bahawalpur jail for a three-month detention.<p>
Pakistan has been in crisis since President Pervez Musharraf, a top U.S. ally, suspended the head of the country's supreme court after he issued several rulings against the government. The issue became a lightning rod for those who want an end to the military control of the government that dates from 1999, when Musharraf, the head of the armed forces, seized power in coup.<p>

