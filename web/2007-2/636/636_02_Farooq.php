<font face="Times New Roman, Times, serif" size="3">Since then, Musharraf, who relies on money and political support from the U.S., has tried to cultivate a democratic image by holding elections and promising to step down as top military commander in the future.<p>
But his high-handed removal of Judge Iftikhar Mohammed Chaudry on March 9 sparked mass demonstrations. Musharraf's armed supporters attacked protesters in Karachi, killing 41 and injuring many.<p>
Farooq Tariq was one of hundreds of activists arrested in early June. His transfer to Bahalapur, eight hours from his home city of Lahore, was personally ordered by the home secretary of the Punjab regional government in order to isolate Farooq from Labour Party members and other allies. As an organizer of the World Social Forum event in Karachi last year, Farooq is widely known as a champion of democracy and workers' rights, in Pakistan and internationally.<p>
Prison conditions in Pakistan are terrible, especially for political detainees. A member of the Pakistan People's Party, Sarmad Mansoor, died of heart attack as a result of being jailed, activists said. Farooq, facing severe restrictions on visits, is vulnerable to similar treatment.<p>
"According to authorities, meeting with Farooq can only be arranged after a written permission from the home secretary, which is a sheer violation of constitutional and fundamental rights," members of the Pakistan Labour Party said in message to supporters.<p>
An international campaign is underway in solidarity with Farooq. Supporters should write to government officials directly to demand his release.<p>

