<font face="Times New Roman, Times, serif" size="5"><b>The senseless death of Edith Rodriguez</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 22, 2007 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">THE BRUTAL police and dilapidated health care system of Los Angeles County are both to blame for another preventable death at LA's King-Harbor hospital.<p>
On May 9, Los Angeles County police brought Edith Isabel Rodriguez into the King-Harbor emergency room after finding her yelling for help in front of the hospital. The 43-year-old mother of three had been released from the emergency room a few hours earlier, her third visit in three days for abdominal pain. She'd been given prescription medication and a doctor's appointment.<p>
Turning to Rodriguez, the nurse said, "You have already been seen, and there's nothing we can do," the <i>Los Angeles Times</i> reported. Parked in the emergency room lobby in a wheelchair after police left, Rodriguez fell to the floor. She lay on the linoleum, writhing in pain for 45 minutes, as staffers worked at their desks and numerous patients looked on. No one helped her.<p>
Arriving to find Rodriguez on the floor, her boyfriend unsuccessfully tried to enlist help from the medical staff and county police. He even called 911--and was told they couldn't help him if Rodriguez was already inside a hospital.<p>
Alerted to the "disturbance" in the lobby, police stepped in--by running Rodriguez's record. They found an outstanding warrant and prepared to take her to jail. Police wheeled her out to the squad car, and when they ordered her to get up, she was dead.<p>
The incident caused an uproar when brought to light by the <i>Times</i> one week later. Linda Ruttlen, the triage nurse involved, has resigned, and the county and the sheriff's department homicide division have launched investigations. But it seems they've already made up their minds to place all the blame on Ruttlen.<p>
Sheriff's Capt. Ray Peavy, who supervises the sheriff's department homicide unit, said that although his investigation is not complete, "The county police did absolutely, absolutely nothing wrong as far as we're concerned." David Janssen, the county's chief administrative officer, said, "I think it's a tragic, tragic incident, but it's not a systemic one."<p>
Janssen is dead wrong. Edith Isabel Rodriguez's tragic death reveals deep, systemic problems with the police and the public health care system. A woman is writhing in pain on a hospital floor--what better time to "protect and serve," if that were the true mission of the police? Yet instead of helping those in need, the police haul them off to jail.<p>
And while the triage nurse certainly could have done more to help Rodriguez, she worked in an extremely overburdened, under-funded public hospital serving 14,000 patients a month, where many, many other patients have been the victims of substandard care.<p>
Since the hospital (formerly known as King/Drew) came under intense scrutiny in 2004, it has lost its national accreditation and federal funding. Hundreds of staff members have been disciplined and services cut. This is clearly a recipe for more tragedies.<p>
Rodriguez was another casualty of a sick system in which the lives of the poor are seen as expendable.<p>
Marcela Sanchez, Rodriguez's sister, said she has been making tamales and selling them to raise money for her sister's funeral and burial. Rodriguez's 25-year-old son, Edmundo, said he still can't understand why his mother died. "It's more than negligence," he said. "I can't even think of the word."<br>
<b>Gillian Russom</b>, Los Angeles<p>

