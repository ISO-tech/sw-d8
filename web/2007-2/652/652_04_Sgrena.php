<font face="Times New Roman, Times, serif" size="3">Here, Sgrena responds to the dismissal of the case and Lozano's accusations.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">ON OCTOBER 25, the Court of Assizes in Rome decided that the trial of Mario Lozano could not go forward because of "lack of jurisdiction."<p>
The objection was one of those raised by lawyer Alberto Biffani, Lozano's defense attorney, and was based on a letter from Colin Powell attached to United Nations resolutions that endorsed the occupation of Iraq. In the letter, it says that the U.S.-led coalition "has the responsibility of jurisdiction over its forces."<p>
This letter has been interpreted by Italian prosecutors and by lawyers representing myself and Mrs. Rosa Calipari as indicating a possible "concurrent jurisdiction" between Italy and the U.S. But since the U.S. has "officially declined its potential active jurisdiction," there was no problem in Italy's exercising its own jurisdiction.<p>
The judge of the Court of Assizes decided differently, but his decision has been regarded as a "serious judicial error" by well-known legal experts such as Antonio Cassese, who presided over the International Court of Justice in the Hague, concerned with the former Yugoslavia.<p>
So we (the injured parties--myself, Rosa Calipari and probably relevant Italian public officials) will appeal to a Court of Cassation to have this decision annulled in order to be able to bring Mario Lozano to trial.<p>
I do not want a scapegoat. The trial must serve to make known what happened in Baghdad on the night of March 4, 2005, and how the killing of Nicola Calipari and the wounding of myself and the other agent came about.<p>
The final word on Lozano's trial has not yet been spoken, and we will pursue every avenue available to us to see that this trial is carried out.<p>
About Lozano's claims:<p>
-- The trial hasn't yet taken place, and nobody has absolved Lozano for what he did. Until now, he has only avoided trial, but he has no reason to feel himself absolved.<p>
Nobody has taken into consideration his photographs (taken by Lozano himself during and after the shooting on March 4, 2005) and his public statements, which certainly would not count in his favor. For now, there is simply a postponement, a temporary delay.<p>
-- On the day when I was abducted, I was doing my work as always, going around and listening to people. On that day in particular, I went to interview refugees from Falluja inside the campus of Baghdad University, as journalists who don't listen only to official sources have done and would have to do.<p>
On the other hand, that same morning, in the same place, there was also a photographer taking pictures for <i>Time </i>magazine. My work is valued by all those people who mobilized to bring about my release, including members of the Italian intelligence service.<p>
It is incredible that Lozano is presenting himself now as a victim. I am the victim, not he; it is he who shot and killed Nicola Calipari, the person who saved my life on two occasions.<p>
-- It's normal that a person who has killed someone, even if carrying out orders, would have psychological problems. It would be worrying if this were not the case.<p>
Fear is also normal; I, too, was afraid after Lozano shot at the car I was traveling in and killed the man who was protecting me. And if Lozano feels threatened, the journalists who are interviewing him should ask him who he feels threatened by. It would be interesting to know.<p>

