<font face="Times New Roman, Times, serif" size="5"><b>The final word has not been spoken</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 9, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">Italian journalist GIULIANA SGRENA was shot and nearly killed by a U.S. soldier in Iraq on March 4, 2005, hours after she was released by Iraqi insurgents who had kidnapped her. Nicola Calipari, the Italian intelligence agent who negotiated her release, was killed while shielding Sgrena from U.S. gunfire that raked their car as they traveled toward the Baghdad airport.<p>
In April, a trial in absentia began in Italy for the U.S. soldier who fired on the car--Spc. Mario Lozano of the U.S. Army's 69th Infantry Regiment. The U.S. refused to hand over Lozano and dismissed all calls from the Italian government for action. Lozano, meanwhile, complained that he was a "victim," and that Sgrena was "making money" off the incident.<p>
Last week, the trial was suspended after a judge ruled that the Italian court didn't have jurisdiction to try Lozano.<p>
Following the dismissal, Lozano blamed Sgrena in a statement: "She went out there, and she wanted to mingle with the terrorists and all that. Then she gets caught. Now we have to send good men to go after this one person who knows that she put herself in the situation...So it's her fault that this is happening, it's not my fault. It's not my fault, it's not America's fault, it's not the Italian government's fault. It's Sgrena's fault."<p>

