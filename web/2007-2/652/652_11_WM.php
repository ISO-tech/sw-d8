<font face="Times New Roman, Times, serif" size="5"><b>Garbage bosses beat union in LA</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 9, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>SARAH KNOPP explains why the Teamsters settled for concessions at Waste Management.</b><p>

A STRIKE of some 500 Los Angeles-area garbage collectors against Waste Management (WM) went down to an unnecessary defeat when workers voted October 31 by a 266-78 margin to accept a contract they had rejected almost two weeks before.<p>
The contract will leave workers in this demanding and dangerous job with far lower pay than their counterparts in other cities--the average wage will be $21.80 an hour after the five-year deal is up.<p>
WM used to pay 44 hours for a week of vacation time to a workforce that survives on overtime. Now, it will pay only 42 hours.<p>
To add insult to injury, 43 workers were fired on returning to the job after the strike ended. Management declared that they had been permanently replaced by scabs.<p>
WM's strategy was to go for the jugular from the beginning. The company has a professional, in-house scabbing operation, called the "Green Team," which one Teamsters official compared to the Blackwater mercenary company working for the U.S. in Iraq. Drivers, many of them WM managers, were flown in from all over the country to drive routes.<p>
Then, a week into the strike, the company sent a letter to workers' houses announcing that they were about to be permanently replaced. WM also threatened to cut off their health care at the beginning of November if they weren't back to work. "They sent the letters to our houses because they wanted to scare our families," said driver Jose Luis.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">THE STRIKE didn't have to end in defeat. Members believed the new leadership of Teamsters Local 396, which represents the workers, would be more aggressive than its predecessors in fighting for a good deal. But local leaders recommended a "yes" vote on the proposed contract when it first came up for a membership vote--and was rejected by a wide margin.<p>
When the same deal was presented to members two weeks into the strike, local leaders argued they couldn't win a contract that brought workers up to the wage levels of other cities because the LA area is less labor-friendly and Allied Waste and Waste Management face tougher competition--and therefore are more cutthroat in union negotiations.<p>
But WM could have afforded a bigger raise. Its $1.1 billion in profits last year works out to almost $25,000 for every single employee.<p>
When members authorized the strike earlier in October, local leaders vowed to fight--but they did very little to organize a winning strategy.<p>
Unlike the victorious battle against WM in the East Bay earlier this year, which won support from the local labor movement, there were no rallies or support campaigns built among LA unions.<p>
There was no effort to build community support. Organizations of the immigrant rights movement--whose mass mobilizations over the past two years have electrified people throughout the LA area--offered support for the strike of this largely immigrant workforce, but Teamsters officials discouraged them from acting.<p>
A union contract with drivers who collect commercial garbage with BFI/Allied Waste expired at the same time as at WM, but preparations for a strike, which would have more than doubled the number on the picket line, were stopped at the last minute. Officials from Local 396, which represents these workers as well, recommended a "yes" vote.<p>
Thus, the strike--or at least the issues of wages and dignity on the job that motivated it--could be ignored by the media.<p>
Worst of all, according to Luis, the Green Team was about to take a hit--under California law, drivers with an out-of-state commercial license can only drive for 10 days. Many of the Green Team scabs were on the cusp of having their time run out when the strike was ended.<p>
"I feel like we weren't really supported," said Luis. "First of all, there's not going to be a next time when our contracts all expire at the same time, because the lengths of the contracts with BFI/Allied Waste and Waste Management are different now. This was a wasted opportunity.<p>
"It seems to me like these are huge national companies, and I don't see any reason not to have a national contract...<p>
"I don't think that this union leadership wanted the strike in the first place, and so they just let this happen...I'm not a political person, but it seems like we have to do something here."<p>

