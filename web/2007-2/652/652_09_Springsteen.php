<font face="Times New Roman, Times, serif" size="5"><b>Searching for a world with some soul</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Jesse Zarley</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 9, 2007 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Bruce Springsteen,<i> <b><a href="http://www.amazon.com/gp/redirect.html?ie=UTF8&amp;location=http%3A%2F%2Fwww.amazon.com%2FMagic-Bruce-Springsteen%2Fdp%2FB000V8I2QU%3Fie%3DUTF8%26s%3Dmusic%26qid%3D1194380637%26sr%3D8-1&amp;tag=socialistwork-20&amp;linkCode=ur2&amp;camp=1789&amp;creative=9325" target="_blank">Magic</a>, </b></i>Sony Records, 2007.</font><p>

<font face="Times New Roman, Times, serif" size="3">AFTER FIVE years apart for the E Street Band, the gang is back. With his October 2 release, <i>Magic, </i>Bruce Springsteen paints an often haunting picture of the bitterness and anger felt by millions of Americans today seeking an alternative to the snake-oil salesmen and women who have trampled civil liberties and fed the public nothing but racism, lies and war.<p>
In the song "Livin' in the Future," Springsteen's character expresses a sense of betrayal built up over the years following the 2004 presidential election:<p>

<blockquote>
<i>The earth it gave away, the sea rose toward the sun<br>
I opened up my heart to you it got all damaged and undone<br>
My ship Liberty sailed away on a bloody red horizon<br>
The groundskeeper opened the gates and let the wild dogs run.</i>
</blockquote><p>

While heavily alluding to Bush in that song, in the solid E Street rocker "Last to Die" Springsteen widens the scope of his scorn declaring that "the wise men were all fools, what to do" and asks the painfully real question: "Who'll be the last to die for a mistake?"<p>
My two favorite tracks on the album, "Gypsy Biker" and "Devil's Arcade," deal with the suffering felt by families of soldiers who died in Iraq or Afghanistan.<p>
The narrator of "Gypsy Biker" tells the story of how a family deals with the death of their son in a war based on blood for oil--"The speculators made their money/on the blood you shed"--by polishing up their son/brother's motorcycle.<p>
Furthermore, he challenges anyone who justifies continued U.S. occupations:<p>

<blockquote>
<i>The favored march up over the hill<br>
In some fools parade<br>
Shoutin' victory for the righteous<br>
But there ain't much here but graves.</i>
</blockquote><p>

With the final track, "Devil's Arcade," a likely continuation of the narrative in "Devils and Dust" from the 2005 album of that title, the character's buddies are dead in the desert, and he now lies wounded in a hospital bed.<p>
Springsteen levels his ire at politicians and members of the media who support the war, clearly don't fight in it, and then use the memory of the dead and wounded to legitimize war: "You said heroes are needed, so heroes get made/Somebody made a bet, somebody paid."<p>
In the title track, Springsteen tells the bleak story of a magician who has used deception to take everything dear to them, prophesizing:<p>

<blockquote>
<i>On the road the sun is sinkin' low<br>
There's bodies hangin' in the trees<br>
This is what will be, this is what will be.</i>
</blockquote><p>

With the ugly racism exhibited recently in Jena, it's hard to disagree with him.<p>
The side of Springsteen revealed in <i>Magic </i>is clearly different than the one who played "No Surrender" for Democratic Party presidential candidate John Kerry rallies in 2004 in a last stand against the greater evil of a potential Bush reelection. Unlike the seminal "Born in the U.S.A." I don't think anyone from either side of the aisle will be leaping to appropriate the songs on this album as no one gets off the hook.<p>
In the words of radical rock journalist Dave Marsh, "Faith, Springsteen seems to be saying, will get you so far, and not a step more. The rest is down to work and struggle, and you'd better not stop, except maybe to dance, and then you'd better make sure you're dancing as well as a man with a pistol pointed at his toes."<p>

