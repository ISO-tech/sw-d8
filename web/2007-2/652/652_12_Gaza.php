<font face="Times New Roman, Times, serif" size="5"><b>Palestinians cut off from basic necessities</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 9, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">ISRAEL HAS begun to restrict imports of gasoline and diesel fuel into Gaza as part of a new campaign of collective punishment against the population of 1.5 million Palestinians.<p>
Despite widespread international condemnation, the cutoffs of 10 to 15 percent of Gaza's fuel began in early November just as winter weather began to take hold. Israel had also planned to cut off 60 percent of Gaza's electricity, but Israeli Attorney General Menahem Mazuz barred implementation of the plan "for the time being because the consequences of such a move on the humanitarian situation in Gaza had not yet been fully calculated," according to the <i>Jerusalem Post</i>.<p>
In reality, the impact is easy to calculate. "Cutting fuel supplies into Gaza will only exacerbate the humanitarian problems that already exist," said Sarit Michaeli, a spokesperson for B'tselem, an Israeli human rights group. "Israel still exercises enormous control over Gaza. Therefore, it has obligations under international law to allow the normal running of everyday life."<p>
Israeli politicians justify the cutoffs as a way to punish the Palestinian people for the firing of Qassam rockets at the town of Sderot in southern Israel. But the rockets are almost totally ineffective.<p>
As Israel's <i>Ha'aretz</i> newspaper noted, "There is an enormous gap between the reasons Israel is giving for the decision to impose significant sanctions against Hamas rule in the Gaza Strip and the real intentions behind them. In practice, defense officials believe that the Palestinian militants will intensify their attacks in response to the sanctions.<p>
"As such, the real aim of this effort is twofold: to attempt a new form of 'escalation' as a response to aggression from Gaza, before Israel embarks on a major military operation there; and to prepare the ground for a more clear-cut isolation of the Gaza Strip--limiting to an absolute minimum Israel's obligation toward the Palestinians there."<p>
The description of the operation by <i>Ha'aretz </i>reporters Amos Harel and Avi Issacharoff suggests that Israeli military planners are preparing for a particularly brutal assault, even by Israeli standards.<p>
"Many soldiers will be killed, and so will many innocent Palestinians, because the IDF [Israel Defense Force] employs a massive artillery bombardment before it sends infantry into the crowded built-up areas," wrote Harel and Issacharoff. "This will be a 'dirty war,' very aggressive, that will have scenes of destruction similar to southern Lebanon in 2006. The sole exception: unlike in Lebanon, the population there has nowhere to run."<p>
Israel's clampdown on the flow of goods into Gaza has already had a devastating impact on Palestinians. The price of flour has risen by 80 percent, sugar by 60 percent and tobacco by 150 percent.<p>
The restriction of imports of building supplies has not only made reconstruction efforts difficult, but caused massive unemployment. The price of concrete has increased by 1,000 percent, and the lack of other raw materials needed for production has effectively halted much of the industrial sector.<p>
Israel has also suspended the flow of essential medical supplies, such as medicines for cardiac patients and the anesthetic nitrous oxide, which has required patients to delay life-saving surgery.<p>
"There is a possibility of a health disaster occurring here anew, with patients dying because our hospitals are unable to conduct surgeries," Bassem Naim, the health minister in Gaza's Hamas-led government, told <i>Al-Ahram Weekly</i>. "There is a need for international intervention to prevent this catastrophe from happening."<p>
According to the head of cardiology at Dar Al-Shifa Hospital, the largest hospital in Gaza, the siege has made it nearly impossible to treat patients because the hospital's five heart-monitoring machines have been in use for 17 years and lack the replacement parts needed to fix them.<p>
"There's no milk in the shop, no medicine in the hospital, and bread is getting more expensive," resident Abu Adli said to his neighbor Fuad in a conversation reported by Gaza blogger Mohammed Omer.<p>
Fuad replied: "Yes, Israel wants to make life impossible for us, and they don't ever open the borders for us to go and find some other solution. The aim is to kill us inside our borders, to starve us to death."<p>

