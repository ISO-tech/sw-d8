<font face="Times New Roman, Times, serif" size="5"><b>Why TV and film writers hit the picket line</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 9, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">TELEVISION AND film writers had hit the picket lines in Los Angeles and New York as <i>Socialist Worker </i>went to press to demand their fair share of Hollywood's rapidly growing revenues in DVD sales, internet downloads and other digital media.<p>
Television and film producers claim they can't agree to a formula for paying writers residuals when their work is downloaded onto computers and iPods, because they don't know how much money they'll make from these "new media." But the Writers Guild of America (WGA) isn't buying that script for a second.<p>
"The big producing companies know that's going to be the source of their revenues, because you can tell they're already trying to circle the wagons and lay claim to all of it," Tina Fey of <i>Saturday Night Live</i> and <i>30 Rock</i> fame explained to a CNN reporter. "We're just asking for a reasonable share of that so we can keep our pension plan going, keep our health fund going. Otherwise our union will just disappear in the next 20 years."<p>
Patrick Mulvihill, a writer-producer and WGA member for three years, explained that "48 percent of guild members at any given time are unemployed."<p>
"The majority of writers aren't rich," he said, as he and fellow members walked the picket line. "When you talk about residuals, that's how you survive--that's how you don't lose your house [between jobs]. If they're going to use our work, we need to get paid for it."<p>
Mulvihill said the strike authorization vote was the largest turnout in the guild's history. "This is a very important negotiation," he said. "We're on the cusp of new technology. New media is huge. We're just asking for fair and equitable compensation for the new platforms. The money is going into the pockets of the conglomerates, not the people who do the work."<p>
The Alliance of Motion Picture and Television Producers (AMPTP), which represents networks and studios, sent a letter last week to other unions in the entertainment industry, reminding them of the "no-strike" clauses in their contracts.<p>
"We expect each union to comply with this no-strike obligation and order your members to work," alliance President J. Nicholas Counter wrote--proving that producers, too, are sometimes capable of writing good comedy.<p>
But the Teamsters, rather than "ordering" their members to work, made an important show of solidarity with the WGA by encouraging Teamsters truck drivers, casting directors and location managers to honor the writers' picket lines.<p>
"As for me as an individual, I won't cross any picket line, whether it's sanctioned or not, because I firmly believe that Teamsters do not cross picket lines," wrote Leo Reed, secretary-treasurer of Teamsters Local 399 in Los Angeles.<p>
WGA West President Patric Verrone told reporters on the eve of the strike: "I hope that our efforts and the efforts of the Teamsters convince this industry that they need us to provide creative content, and that without us, yes, effectively they don't have a product, and they can't work."<p>
The WGA anticipates a tough fight. Expect an avalanche of unscripted "reality show" drivel to come pouring out of your TV set as the media corporations hope to wait out the writers.<p>
"We don't want [the strike] to happen, but the thing we want less is a bad contract," said Verrone. "Whatever it takes to get a good contract is what we intend to do, for as long as it takes."<p>

