<font face="Arial, Helvetica, sans-serif" size="2"><b>EDITORIAL</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Who's being excluded from equal rights?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 9, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">LEISURE SUITS were in, gas was 55 cents a gallon, and Barbra Streisand's <i>The Way We Were</i> topped the charts when legislation to end discrimination against gays in the workplace was first proposed--in 1974.<p>
Despite polls showing that 85 percent of Americans support equal opportunity in the workplace, 33 years have passed without Congress outlawing employment discrimination on the basis of sexual orientation and gender identity.<p>
Only 19 states have laws expressly barring discrimination against gays and lesbians in public and private workplaces, and another 11 protect only public employment. That leaves 20 states where it is perfectly legal for people to be fired on the mere suspicion that they are gay, lesbian, bisexual, transgendered or any other sexual minority.<p>
The current Democratic-controlled Congress has pledged--once again--to pass legislation to reverse this inequity. Yet openly gay Rep. Barney Frank (D-Mass.), author of the proposed Employment Non-Discrimination Act (ENDA), removed language protecting transsexuals from his bill, claiming that keeping it would weaken support.<p>
In response, more than 300 lesbian, gay, bisexual and transgender (LGBT) organizations withdrew their backing for ENDA. "Nobody should be cut out or left behind simply because Democratic leadership is too impatient to round up the votes needed to pass a comprehensive bill," said Christopher Anders, senior legislative counsel for the ACLU.<p>
"We are greatly disappointed that the committee chose to move forward with a bill that is not endorsed by a single LGBT organization," said Mara Keisling of the National Center for Transgender Equality.<p>
Frank's reaction? "There is a tendency in American politics for the people who feel most passionately about an issue, particularly ones that focus on a single issue, to be unrealistic in what a democratic political system can deliver, and that could be self-defeating," Frank told the <i>New York Times</i>. Translation: Don't expect any accountability to the Democrats' liberal supporters.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">A 2006 study conducted in the San Francisco Bay Area with 194 transgender individuals found a 35 percent unemployment rate, with 59 percent earning less than $15,300 annually.<p>
Nationwide, studies by the Williams Institute reveal that up to 47 percent of transgender people have been denied employment, up to 56 percent have been fired for their gender identity, and between 22 and 31 percent have been either verbally or physically harassed in the workplace.<p>
ENDA, as it is currently worded, would make it illegal for employers to discriminate on the basis of sexual orientation in hiring, firing, promoting or paying an employee. However, individuals working for small businesses and religious institutions, and uniformed members of the military would be excluded from protection.<p>
Rep. Tammy Baldwin (D-Wis.), the only other openly gay member of Congress, has proposed an amendment that would reinstate gender identity to the legislation. But with or without gender identity language, the White House is reportedly planning to veto ENDA.<p>
While all the leading Democratic presidential contenders--Hillary Clinton, Barack Obama and John Edwards--verbally support ENDA, none has lifted a finger to promote it on the campaign trail. And none of the three support equal-marriage rights.<p>
There is one gay issue that all the leading Democratic contenders have been vocal about--reversing the "don't ask, don't tell" law, passed during Bill Clinton's administration, which bars gays and lesbians from serving openly in the military.<p>
In other words, serving openly in the fight for oil and empire appears to be a higher priority than serving openly at McDonalds.<p>

