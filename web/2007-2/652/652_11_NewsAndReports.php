<p><a name="Cat"></a><font face="Times New Roman, Times, serif" size="4"><b>Protest against Caterpillar</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Brian Huseby</b></font><p>

<font face="Times New Roman, Times, serif" size="3">OLYMPIA, Wash.--Approximately 30 people here gathered October 19 to tell Caterpillar Inc. to stop profiting from Israel's violations of the human rights of Palestinians.<p>
Since 1967, Israel has used Cat bulldozers to demolish nearly 12,000 Palestinian homes, leaving approximately 70,000 Palestinians homeless and killing at least 17. In September, teenager Mamoud al-Kifafi was killed by the blade of a Cat bulldozer during the demolition of 13 Palestinian homes.<p>
"A lot of people don't realize that this is still going on," Cindy Corrie, the mother of activist Rachel Corrie, the Olympia native and activist who was killed by a Cat bulldozer in 2003, said in an interview. "[W]e are trying to prevent [another death] from happening."<p>

