<a name="CEDP"></a><font face="Times New Roman, Times, serif" size="4"><b>Campaign to End the Death Penalty</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Julien Ball</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--Exonerated death row prisoners and family members of current and former prisoners will join activists from around the country here November 10-11 for the Campaign to End the Death Penalty's seventh annual convention, "The Death Penalty: Guilty on All Counts!"<p>
Sister Helen Prejean, the prominent opponent of the death penalty and author of <i>Dead Man Walking</i>, will be the keynote speaker at an evening meeting November 10. The program is cosponsored by the American Civil Liberties Union of Illinois and the University of Chicago, and the Rockefeller Memorial Chapel.<p>
Other speakers include exonerated death row prisoners Shujaa Graham, Darby Tillis and Harold Wilson; Barbara Becnel, the advocate of executed California death row prisoner Stan Tookie Williams; Martina Correia, sister of Georgia death row prisoner Troy Davis; Derrel Myers of Murder Victims' Families for Human Rights; and Jeremy Scahill, author of the<i> </i>bestselling <i>Blackwater: The Rise of the World's Most Powerful Mercenary Army</i>.<p>
The convention comes at an important time for the anti-death penalty movement. The Supreme Court recently agreed to take up the question of whether the lethal injection procedure currently used in 37 states constitutes cruel and unusual punishment--which has led to a temporary halt on executions across the country.<p>
At the same time, death penalty activists are celebrating successful efforts to stop the execution of Texas death row prisoner Kenneth Foster Jr. His father, Kenneth Foster Sr., will be present at the convention.<p>
<i>For information about how to register for the convention, call 773-955-4841, e-mail <a href="mailto:cedp@nodeathpenalty.org">cedp@nodeathpenalty.org</a> or visit <a href="http://www.nodeathpenalty.org" target="_blank">www.nodeathpenalty.org</a></i>.<p>
