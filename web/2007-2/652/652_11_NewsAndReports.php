<p><a name="War"></a><font face="Times New Roman, Times, serif" size="4"><b>No to war and occupation</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul Hubbard</b></font><p>

<font face="Times New Roman, Times, serif" size="3">PROVIDENCE, R.I.--Nearly 200 students, activists and community members turned out to hear Dahlia Wasfi's compelling presentation "Life in Iraq Under U.S. Occupation" at Brown University October 23. The event was sponsored by Operation Iraqi Freedom at Brown University, the American Friends Service Committee, the Rhode Island October 27 Coalition and the International Socialist Organization.<p>
Wasfi's presentation showed how the already difficult lives of the Iraqi people have deteriorated since the U.S. war and occupation began. She was joined on stage by Ted Goodnight, an Afghanistan war veteran and member of Iraq Veterans Against the War.<p>
In visits to Iraq in 2004 and 2006, Wasfi said, "[I]t was significantly noticeable how life conditions had deteriorated during that time, in particular the security situation, the availability of electricity, the availability of potable water."<p>
Wasfi called for the immediate withdrawal of all U.S. forces. "[W]e as Americans have more in common with the Iraqi people than we do with our own government," she said.<p>
<i>Kathy Lessuck contributed to this report</i>.<p>
