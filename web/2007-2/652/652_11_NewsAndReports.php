<p><a name="Health"></a><font face="Times New Roman, Times, serif" size="4"><b>Seattle forum for health care rights</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By John Dimond and Kate Johnson</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--About 80 people gathered at Seattle Central Community College for an October 22 panel discussion titled "The Sicko Cure: Building a Movement for Real Health Care Reform."<p>
The panel, the first major event of the Right to Health Care Now! (RHCN) coalition since it was formed in July, was able to bring together many of the leading health care groups and activists in the state, including Physicians for a National Health Program (PNHP), Seattle National Organization for Women, Health Care for All Washington, and Physicians for Social Responsibility.<p>
Dr. James Floyd of PNHP blasted corporations for diverting dollars away from patient care into the hands of administrators. A 1999 study showed that 31 percent of health care spending in the U.S. ends up paying for administrative costs, while in Canada, only 17 percent is spent on these costs.<p>
Dr. Sherry Weinberg, a retired pediatrician, addressed "universal coverage" reforms like the new law in Massachusetts, which seeks to criminalize those who don't buy private insurance plans. This model is being trumpeted by many of the leading Republican and Democratic candidates in the 2008 election.<p>
Kate Johnson of RHCN and the International Socialist Organization addressed the need to build a grassroots movement based on activism, coalition-building and mass action. "This model of organizing was how the civil rights movement was able to end segregation and pave the way for the passage of Medicare and Medicaid in 1965, the most significant health care reform won in U.S. history," she said.<p>
RHCN plans to bring Donna Smith, who is a featured patient in SiCKO, to Seattle for the first screening of the movie in the area.<p>
