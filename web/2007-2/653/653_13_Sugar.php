<font face="Times New Roman, Times, serif" size="5"><b>Reaching for justice in the cane fields</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Cindy Beringer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 16, 2007 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i><a href="http://www.thepriceofsugar.com/" target="_blank">The Price of Sugar</a>, </b></i>a documentary by Bill Haney. Visit <a href="http://www.thepriceofsugar.com/" target="_blank">www.thepriceofsugar.com</a> for local screenings.</font><p>

<font face="Times New Roman, Times, serif" size="3"><i>THE PRICE of Sugar,</i> a documentary that calculates cost in terms of human misery, will leave you with a bitter taste that's hard to shake.<p>
When Father Christopher Hartley arrives in the Dominican Republic in 1997, no one is allowed into the <i>bateys, </i>the slum compounds within the plantation of the powerful Vicini family. When he manages to get in, he finds brutal conditions of virtual slavery that transform him into a one-man crusade for justice.<p>
Hartley travels to the border where every year 30,000 Haitians are recruited for the cane harvesting season, with the promise of a decent salary. At the plantation, they are paid less than 90 cents a day--in script they must use at the overpriced company store.<p>
The children are malnourished and plagued by parasites. They have no clean water, health care or education. When the Haitians arrive in the Dominican Republic--before dawn so they can't see where they are and escape--they are stripped of their documents and papers.<p>
Vicious armed guards patrol the grounds. If anyone manages to escape, he can be arrested at any time as "illegal." Even worse, perhaps, would be his reception among the Dominicans where fear of "Haitianization" has been carefully nurtured for generations.<p>
The Vicini family rules the country "like an invisible hand that controls everything," and both dictators and presidents do its bidding. Hartley took photographs for several years to document what he found in the <i>bateys. </i>Because Hartley bucked a centuries-old taboo and brought in American doctors and aid workers, conditions have improved slightly for the Haitians.<p>
Eventually, Hartley shows workers how to organize and to refuse to work until they are told what they will be paid. Hartley has received several death threats, and the Vicini family warns the workers that they will know who is in charge when the father leaves. A main organizer among the Haitian workers is fired and thrown out without papers or hope for employment.<p>
Documentary filmmaker Bill Haney arrived at the plantation with American aid workers delivering medical supplies and filmed for two years at Hartley's request. <i>The Price of Sugar </i>has been shown at film festivals around the globe and is currently popping up in scattered towns and cities around the U.S.<p>
Typically, the church accommodated the Vicinis by reassigning Hartley when the film was completed. The Vicini family has hired top-notch lawyers and a public relations firm to stop the documentary's release.<p>
Specific instances of slavery and the use of child labor under horrible conditions has become a cause c&eacute;l&egrave;bre in the 21st century--examples including the film <i>Trade </i>on the sex industry and John Bowie's book <i>Nobodies </i>on forced labor in the U.S.<p>
Father Hartley's crusade is a noble one, but the blood and suffering of the cane cutter has been intertwined with the price of sugar since the first sugar grower tried to make a profit. The answer doesn't lie in isolated campaigns, boycotts or "fair trade" commodities.<p>
The most hopeful moment in the film occurs when paid protesters converge on the church in an attempt to drive out the priest. Inside Haitian cane cutters and Dominican supporters sing the Spanish equivalent of "We Shall Not Be Moved," and it is the protesters who are driven out.<p>
The suffering of workers around the world--both documented and not--will end when workers themselves find the means to end the vicious profit-driven system that enslaves them.<p>

