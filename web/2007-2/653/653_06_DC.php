<font face="Arial, Helvetica, sans-serif" size="2"><b>VIEWS AND VOICES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Taxation without representation</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 16, 2007 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">THE 600,000 residents of the District of Columbia pay the nation's second highest per capita federal income taxes, but they have no vote in Congress. Their license plates say it all: "Washington, D.C.: Taxation without Representation."<p>
"Consider the war in Iraq," writes <i>Nation</i> editor Katrina vanden Heuvel. "D.C. residents have funded it to the tune of $1.6 billion and have lost three soldiers with 15 more wounded."<p>
Last month, Senate Republicans took the wind out of the sails of a spirited voting rights campaign that had begun with renewed energy in January.<p>
D.C.'s non-voting House delegate Eleanor Holmes Norton reintroduced legislation in January, revitalizing a movement organized primarily by the group D.C. Vote. With the support of D.C. Mayor Adrian Fenty--as well as Senate Majority Leader Harry Reid (D-Nev.) and Sen. Orrin Hatch (R-Utah) coming out in favor of the D.C. Voting Rights Act--the campaign gained momentum through the spring and into the summer.<p>
Characterized by upper-level, mostly Democratic, support, coupled with grassroots mobilization, the movement saw its first victory in April. Days after a thousand demonstrators marched through the District, the House of Representatives passed the D.C. Voting Rights Act.<p>
Next, in June, the Senate Homeland Security Committee passed the act, putting it onto the floor for a full vote. Further rallies were called, and partisan skirmishes followed, with a veto promised by President Bush.<p>
Detractors, such as Senate Minority Leader Mitch McConnell (R-Ky.), argued that it was "unconstitutional" for a non-state to be able to vote in Congress. The bill was designed to assuage partisan concerns, giving an extra House seat to largely Republican Utah to offset the seat created for majority Democrat D.C. It left the Senate unchanged.<p>
But the trade-off was no comfort to Republicans, who perhaps feared the act would encourage D.C. residents to claim the right to two Senators as well. "We are outraged that a minority of Senators, led by Senators Mitch McConnell and Trent Lott, prevented the majority from voting on our bill," said Ilir Zherka, executive director of D.C. Vote. "McConnell and Lott chose to filibuster a bill extending democracy at home at a time when they are pushing for democracy in Iraq."<p>
But blaming Republicans should not translate into relying on Democrats, as some have suggested. There may be more support ahead in 2009, but a thousand voting rights activists saw in April what they can do themselves right now.<p>
The United Nations Human Rights Committee this year called Washington, D.C.'s lack of representation a human rights violation. Over a century ago, Frederick Douglass stated, "Regarding their political rights, residents of the nation's capital are not really citizens but practically aliens in their own country."<p>
No taxation without representation! Give D.C. voting rights now!<br>
<b>Chris Yarrison</b>, Washington, D.C.<p>

