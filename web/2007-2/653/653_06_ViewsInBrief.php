<a name="Honest"></a><font face="Times New Roman, Times, serif" size="4"><b>The movement needs an honest assessment</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IN THEIR criticism of an article by Sharon Smith on the state of the antiwar movement (<a href="http://www.socialistworker.org/2007-2/649/649_03_TooLate.shtml" target="_blank">"Too late and too little,"</a> October 19), Khalil Iskarous and Naveen Jaganathan (<a href="http://www.socialistworker.org/2007-2/650/650_06_ViewsInBrief.shtml#October" target="_blank">"Mobilizing for October 27,"</a> October 26) make some serious mistakes, both in their facts and their analyses.<p>
First, the facts. In contrast to their contention, the October 27 coalition in the Bay Area, which the ISO participated in, remained a very narrow organization, mostly comprised of small sectarian groups (even libertarians) and dominated by ANSWER.<p>
United for Peace and Justice (UFPJ) did join in, unlike in the past, but its organized forces in the Bay Area are very weak in terms of mobilizing for protests. One positive note was the active participation of labor, which resulted in a labor contingent of about 300 union members.<p>
As for their charge about the timing of Smith's article, <i>Socialist Worker </i>has consistently analyzed and critiqued the repeated failures of UFPJ to mobilize antiwar sentiment, so the complaint that Smith should have written before the war-funding vote is unfounded (see, for example, <i>SW's </i>editorial in its September 7 issue, "How is Bush getting away with it?").<p>
Now, to deal with the errors of analysis. Smith's article used events in Chicago to illustrate general conclusions about the trend of the antiwar movement, which <i>are </i>representative of the national picture.<p>
If you add up the total number of protesters at the major demonstrations throughout the country (even using figures put out by UFPJ, which many activists thought were inflated), you find that October 27 was one of the weakest days of protest since the war began. And this at a time of record high antiwar sentiment!<p>
On the charge of the ISO being "irresponsible" for not mobilizing all-out for October 27 when "activists have spent a tremendous amount of time and energy organizing for it"--the ISO should be counted among those activists, and I can speak for the Bay Area in saying that we made every effort to bring out our membership and allies.<p>
The heart of the matter, though, is whether simply cheerleading and printing all-out calls are what's needed to make the movement and the demonstrations larger. The antiwar movement faces challenges and frustrations that require much more.<p>
The ISO--as well as any other activist or political organization that wants to end this war--has a responsibility to provide a sober assessment of the strengths (and weaknesses) of the movement, a political explanation for why this is so, and a set of politics and tactics that can help rebuild the base of the movement.<p>
I won't recount all the articles over the last several weeks that have addressed these issues, but I will say that <i>Socialist Worker </i>should be proud of its work.<br>
<b>Sid Patel, </b>San Francisco<p>
