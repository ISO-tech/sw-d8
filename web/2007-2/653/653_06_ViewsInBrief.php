<p><a name="Health"></a><font face="Times New Roman, Times, serif" size="4"><b>Who should pay for health care?</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AS BUSH gets ready to again veto the bill for funding children's health care in the form of the State Children's Health Insurance Program (SCHIP), we shouldn't let the Democrats off the hook either.<p>
Not only does the funding for this program come from increasing cigarette and cigar taxes, a regressive tax that burdens the working class disproportionately to the rich, but it also shows who they are really concerned about in our society.<p>
If the Democrats really cared about children's health, or anybody's health--other than their own in the political polls--they would be putting forward a plan for single-payer health care and would stop nickel-and-dime-ing the working class to death.<p>
Many of the same people who hope to benefit from SCHIP will also be forced to pay this new cigarette and cigar tax, not to mention all the other consumption taxes we already have to pay. According the U.S. Department of Health and Human Services, over one-third of smokers are in families that make less then the national median income and around half of smokers are in families earning less than 200 percent of the federal poverty line.<p>
The real solution to the shortage of money for health care, education, jobs and all other essential services for a healthy society is to stop spending over 50 percent of the U.S. budget on the military, and instead give it back to our communities. We need to be taxing the rich to fund programs for the people, but that won't happen with Corporate America's "B team" in the majority--and we've seen that clearly now.<p>
Bush's veto of a bill to help fund health care for children shows which side he's on, but the Democrats' cynical move of making some of the lowest-paid members of the working class pay for SCHIP should not go unchallenged either.<p>
We should not be fooled into backing a plan for children's health to be funded by other working-class people. Instead, we should demand a tax on the corporations, millionaires and billionaires to pay for a single-payer health plan for all of us!<br>
<b>Erik Wallenberg</b>, Seattle<p>

