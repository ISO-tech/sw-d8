<font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Chewed up and spit out by the war machine</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 16, 2007 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>SHARON SMITH explains that soldiers returning home face a new battle--confronting endless bureaucratic red tape.</b><p>

AT LAST weekend's official Veterans Day celebrations, some vets were obviously more welcome than others.<p>
In Boston, the crowd at an American Legion-sponsored event turned hostile when a dozen members of Veterans for Peace refused to move away from a podium while protesting their exclusion, leading to their arrest.<p>
In Long Beach, Calif., the local Veteran's Day parade committee barred members of Iraq Veterans Against the War (IVAW), Veterans for Peace and Military Families Speak Out--simply because their organizations oppose the Iraq war.<p>
The Long Beach parade committee accused the antiwar vets and their families of promoting a "political agenda." But parade coordinator Martha Thuente unwittingly betrayed the committee's own political agenda, telling the <i>Long Beach Press-Telegram</i>, "We do not want groups of a political nature, advocating the troops' withdrawal from Iraq."<p>
Responding to his exclusion from the Long Beach parade, Jason Lemieux, a 24-year-old Marine who served three tours of duty in Iraq before joining IVAW, said, "I wanted to march like the rest of the Iraq veterans."<p>
Certainly every soldier has earned that right--along with the right to oppose the war aims that U.S. troops are being sent to fight and die for.<p>
This point was illustrated vividly by seven active-duty troops who authored a <i>New York Times</i> op-ed article on August 19 called "The war as we saw it." The soldiers, expressing their views "at the tail end of a 15-month deployment" with the 82nd Airborne Division, exposed the "farfetched" goals of the U.S. occupation of Iraq.<p>
"We see that a vast majority of Iraqis feel increasingly insecure and view us as an occupation force," they wrote, adding that Iraqis "will soon realize that the best way to regain dignity is to call us what we are--an army of occupation--and force our withdrawal."<p>
As they were writing the article, one of its authors, Staff Sgt. Jeremy Murphy, was shot in the head and flown to a military hospital in the U.S.. Three weeks later, two more of the authors, Sgt. Omar Mora and Sgt. Yance T. Gray, were killed in a vehicle accident in western Baghdad--just as Gen. David Petraeus was readying his upbeat report on the troop surge to Congress.<p>
Already, 2007 has broken records as the deadliest year for U.S. troops in both Iraq and Afghanistan.<p>
This fact isn't lost on the troops themselves. The <i>Los Angeles Times</i> reported that a mental health survey of U.S. troops serving in Iraq, released in May, showed that 45 percent of soldiers surveyed ranked morale in their unit as low or very low, while only 7 percent ranked it high or very high.<p>
As <i>Guardian</i> reporter Peter Beaumont reported from Baghdad on August 12: "[T]hese days, the war in Iraq is defined by different ways of expressing the idea of being weary. It is a theme that is endlessly reiterated as you travel around Iraq. 'The army is worn out,' says a soldier working for the U.S. Army public affairs office, who is supposed to be telling me how well things have been going since the 'surge' in Baghdad began."<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">NOT SURPRISINGLY, soldiers who have endured multiple Iraq deployments, now the norm, are 50 percent more likely than those with one tour to experience acute combat stress.<p>
But on returning home, they face a new battle to return to civilian life, confronting endless bureaucratic red tape at every turn.<p>
The Veterans' Administration (VA) admits it has a backlog of 600,000 disability claims--including 250,000 from veterans of the current wars in Iraq and Afghanistan. Paul Sullivan from Veterans for Common Sense explained that returning vets seeking help "run into a 26-page claim form, a bureaucracy completely overwhelmed, and at hospitals, if the vets show up suicidal, in some cases, they are turned away."<p>
Income limits for VA benefits instituted in January 2003 leave many thousands of vets uncovered. In many regions of the U.S., a veteran earning as little as $24,000 doesn't qualify for VA health coverage.<p>
Just in time for Veterans' Day, the Washington, D.C.-based National Alliance to End Homelessness released a report documenting that, while veterans constitute just 11 percent of the population, they are 26 percent of homeless people nationwide.<p>
Vietnam vet Arthur Williams, a homeless Vietnam vet from New York, told <i>Voice of America</i>, "My problems [are] related to my experiences in Vietnam, and all the carnage and the mayhem and the suffering that I've seen...And it has a profound effect on my head. I killed and maimed."<p>
Indeed, as IVAW member Jeff Englehart wrote in the <i>GI Special</i> on November 9, " For some of my closest friends and me, Veterans Day is every day, and it is a fact that cannot be so easily praised and cheered with the enthusiastic waving of an American flag.<p>
"Many of us live through life in the solitary confines of our experiences, walking amongst the living, while holding hands with the dead...<p>
"But if we truly believed in recognizing the services of our veterans, we as a people would honor our vets by removing them from the immorality of a criminal war, and demand an immediate and unconditional withdrawal of American troops from Iraq and Afghanistan."<p>

