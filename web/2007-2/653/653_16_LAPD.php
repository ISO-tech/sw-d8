<font face="Times New Roman, Times, serif" size="5"><b>LAPD plan for racial profiling of Muslims</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Danielle Heck</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 16, 2007 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">THE LOS Angeles Police Department has a notorious record of harassment and violence toward African Americans and Latinos. Now, it's setting its sights on Muslims.<p>
A plan by the LAPD to "map" the Muslim community--in reality, racial profiling by another name--is stirring anger at Islamophobia and sparking plans for protests.<p>
On October 30, in testimony before a U.S. Senate committee, Commander Michael Downing of the LAPD's Counter-Terrorism/Criminal Intelligence Bureau revealed the department's plan for identifying where Muslims live in LA--which the LAPD is calling "mapping."<p>
"The mapping of Muslim communities as part of counter-terrorism efforts seems premised on the faulty notion that Muslims are more likely to commit violent acts than people of other faiths," reads a letter drafted by the ACLU of Southern California, Muslim Advocates, Islamic Shura Council and Council on American-Islamic Relations.<p>
"Singling out individuals for investigation, surveillance and data-gathering based on their religion constitutes religious profiling that is just as unlawful, ill-advised and deeply offensive as racial profiling."<p>
The outcry over "mapping" grew so great last week that the LAPD's hoped-for partner in the project, the University of Southern California's National Center for Risk and Economic Analysis of Terrorism Events, said it was studying whether to join up.<p>
Downing's testimony in Congress shows why Muslim and Arab groups are alarmed.<p>
"While this project will lay out the geographic locations of the many different Muslim population groups around Los Angeles," Downing said, "we also intend to take a deeper look at their history, demographics, language, culture, ethnic breakdown, socio-economic status, and social interactions.<p>
"It is our hope to identify communities, within the larger Muslim community, which may be susceptible to violent ideologically based extremism and then use a full-spectrum approach guided by an intelligence-led strategy."<p>
Downing went on to explain that intelligence agencies have "learned" Muslim communities in the U.S. are mistrustful of the mainstream media and therefore may turn to alternative sources of information on the Internet, which make it easier to contact like-minded individuals.<p>
In other words, anyone critical of the U.S. policy of war and occupation in the Middle East--even anyone who wants more information--is suspect.<p>
LA Mayor Antonio Villaraigosa, hailed by liberals as a great hope for revitalizing the Democratic Party, defended police. "Chief Downing has good intentions here," he said.<p>
Downing clearly has intentions, but they aren't "good." In an interview with National Public Radio, he encouraged people to call police if they observe any "suspicious activity" among their Muslim neighbors. Right-wing radio hosts John and Ken--who initiated a "Kill Tookie Hour" on their show during the struggle to save California death row prisoner Stan Tookie Williams, who was executed in December 2005--asked listeners to call in and report where Muslims hang out, so the information can be passed on to the LAPD.<p>
Villaraigosa's attitude is no surprise, given that the Democrats have gone along with virtually every repressive measure proposed to fight the "war on terror" at home. On October 23, for example, the Democratic-controlled U.S. House voted almost unanimously for the "Violent Radicalization and Homegrown Terrorism Prevention Act."<p>
The vaguely written measure takes significant steps toward criminalizing radical thoughts and beliefs--as opposed to actions.<p>
"The broad wording of the bill leaves open many questions," <i>In These Times</i> journalist Lindsay Beyerstein reported. "If homegrown terrorism is defined to include 'intimidation' of the United States government or any segment of its population, could [that mean] investigating groups advocating boycotts, general strikes or other forms of non-violent 'intimidation?'"<p>
In LA, activists are planning a response to publicly demonstrate their opposition to this latest attack on the Arab and Muslim communities.<p>

