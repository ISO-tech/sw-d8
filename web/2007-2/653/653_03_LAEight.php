<font face="Times New Roman, Times, serif" size="5"><b>Government gives up on persecution of LA Eight</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 16, 2007 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">A 20-year-long struggle has finally ended in victory for Palestinian activists targeted by the U.S. government in one of the country's longest-running deportation cases.<p>
Late last month, the Board of Immigration Appeals (BIA) dismissed all charges against Khader Hamide and Michel Shehadeh. The two are members of the LA Eight, a group of seven Los Angeles area-based Palestinian activists and the Kenyan-born wife of one. The eight were arrested in January 1987 and accused of having ties to the Popular Front for the Liberation of Palestine (PFLP). All denied being members.<p>
In a case that tested the rights of non-citizens to freedom of speech, the eight were arrested at gunpoint and held for weeks in maximum security prison after the government accused them of distributing newspapers, holding demonstrations and organizing humanitarian aid fundraisers to aid the Palestinian cause. The government argued that these actions supported the PFLP, and the eight, therefore, should be deported.<p>
Early on, the government focused on Hamide and Shehadeh, the only two of the eight with permanent resident status. They were originally charged with being associated with a Communist organization, but when a court later declared such charges unconstitutional, the government filed new allegations of material support for a terrorist group.<p>
In all, the case went before the U.S. Court of Appeals four times, the Supreme Court once, and the BIA multiple times.<p>
In January, an immigration judge ruled that the government had denied Hamide and Shehadeh due process by refusing to turn over evidence favorable to them and essentially keeping them in legal limbo for so many years. In his opinion, Judge Bruce Einhorn called the proceedings "a festering wound on the body of respondents and an embarrassment to the rule of law."<p>
The BIA has now dismissed the case at the request of the government, which agreed in a settlement to drop all charges and to not seek the removal of either Hamide or Shehadeh in the future based on any of the political activities or connections at issue in the case. Hamide and Shehadeh have agreed not to apply for citizenship for three years.<p>
"This is a monumental victory for all immigrants who want to be able to express their political views and support the lawful activities of organizations in their home countries fighting for social or political change," said Marc Van Der Hout, of the National Lawyers Guild, in a statement.<p>
"Hamide and Shehadeh did nothing more than advocate for Palestinians' right to a homeland, and support charitable causes and other legal activities in the Occupied Territories. That should never have been cause for deportation charges in the first place.<p>
"The government's attempt to deport them all these years marks another shameful period in our government's history of targeting certain groups of immigrants for their political beliefs and activities."<p>
As Hamide told a reporter, "My family and I feel a tremendous amount of relief today. After 20 years, the nightmare is finally over. I feel vindicated at long last."<p>

