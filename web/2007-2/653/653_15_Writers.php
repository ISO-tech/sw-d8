<font face="Times New Roman, Times, serif" size="5"><b>Unions rally behind the writers</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 16, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>RANDY CHILDS and CINDY KAFFEN report that the writers' strike is starting to hit TV companies.</b><p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES--More than 4,000 striking members of the Writers Guild of America-West and their supporters rallied on November 9 outside of Fox Studios. The crowd was fired up as strikers came together for the first time in one place since the WGA's strike against the Alliance of Motion Picture and Television Producers (AMPTP) began November 5.<p>
Hundreds of members of the Screen Actors Guild (SAG), Teamsters and other entertainment industry unions turned out as well to show their solidarity with the writers. SAG members are regularly walking the writers' picket lines, and many Teamsters have refused to cross.<p>
On the eve of the strike, the AMPTP boasted of having months of episodes of their television programs "in the can"--enough to drag out the strike and make the writers give up. But according to <i>Media Life </i>magazine, "It's looking like a lot of the tough talk on the part of the AMPTP was just that. The TV industry is fast shutting down."<p>
Nighttime talk shows like <i>The Tonight Show with Jay Leno</i>, dependent on staffs of writers for topical humor, went straight to reruns. Leno has visited the picket line every day, bringing doughnuts for the strikers. He told <i>Variety</i>, "There aren't many unions left--we have to stick together."<p>
Six primetime series, including ABC's highly rated <i>Desperate Housewives</i>, shut down the first week. NBC's comedy <i>The Office</i> will air its last fresh episode this week, because the show's actors, including star Steve Carell, refuse to cross the writers' picket lines.<p>
"We're shutting down production," WGA West President Patric Verrone told the roaring crowd at the Los Angeles rally, "and we're kicking corporate ass!"<p>
WGA West Executive Director David Young told the crowd, "Brothers and sisters, we're not going to screw this up. We're part of the broader struggle of the American middle class. It's time for us to take a stand."<p>
Other speakers at the rally included Rev. Jesse Jackson, <i>Family Guy </i>creator Seth MacFarlane and producer Norman Lear. Tom Morello and Zack de la Rocha of Rage Against the Machine performed music.<p>
"Billions of dollars are being made, but not for the middle-class writers and actors," SAG President Alan Rosenberg said at the rally. "These corporations worry about profit margins. We worry about paying the bills.<p>
"It's been three decades of rollbacks and givebacks. We can't afford another 30 years of this. We have to draw the line. SAG stands with you shoulder to shoulder, as long as it takes."<p>
Since the "corporate ass" that the writers are kicking belongs to massive conglomerates that also own most news outlets, it's not surprising that the media have been whipping up a backlash against the WGA.<p>
And the same production companies that regularly abuse and neglect everyone who works for them have suddenly conjured up big, wet crocodile tears to shed for everyone, from production crews to office staff to caterers, who will lose income during the strike.<p>
Telepictures Productions, which produces <i>The Ellen DeGeneres Show</i>, played this card in a press release defending its star's decision to continue production without her WGA staff by writing the show herself. "We have asked Ellen to come back to work to fulfill her contractual obligation...to host the show," the company said. "We also wish to preserve the 135 jobs of the staff and crew whose livelihoods depend on the show continuing. We regret the Writer's [sic] Guild has chosen to strike."<p>
As if Telepictures, a subsidiary of Time Warner, the world's largest media empire, would think twice about dumping all 135 of those people if the show was losing money!<p>
The hard, sad fact is that DeGeneres, like all scabs, is doing the dirty work of bosses who are trying to squeeze every penny of profits they can out of their employees.<p>
But as long as people can see through the corporate lies and support the WGA picket lines, a victory for the writers--and the labor movement--is within reach.<p>

