<p><a name="Dictator"></a><font face="Times New Roman, Times, serif" size="4"><b>The dictator that Bush supports</b></font><p>

<font face="Times New Roman, Times, serif" size="3">HOW MUCH longer will George W. Bush and his administration continue to support the Pakistani dictator Pervez Musharraf? Musharraf overthrew a democratically elected prime minister in a military coup, and declared himself president. Then he was "elected." Of course, he didn't have any opponents running against him.<p>
In recent weeks, Musharraf has cracked down on the media, suspended a chief justice, detained hundreds of opposition party members, and banned the gathering of five or more people.<p>
Pakistan has weapons of mass destruction, and I keep hearing that Osama bin Laden (remember him?) is hiding out along the Pakistani border. But President Bush will continue to support Musharraf because he (sort of) supports the "Global War on Terror." I wonder which side "our" government would be on if there was a "Global War on Dictatorships"?<p>
Our country should support civilian democracies and oppose military dictatorships.<br>
<b>Chuck Mann</b>, Greensboro, N.C.<p>

