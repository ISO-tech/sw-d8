<a name="Iraqi"></a><font face="Times New Roman, Times, serif" size="4"><b>The politics of Iraqi labor</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THIS MONTH, U.S. Labor Against the War, United for Peace and Justice and other organizations sponsored an "Iraq Labor Tour" in various U.S. cities. One of the featured speakers represents the Iraq Federation of Oil Workers, which spearheads opposition to privatization of Iraqi oil and demands immediate U.S. withdrawal.<p>
However, the tour also includes a representative of the General Federation of Iraqi Workers (GFIW). The GFIW (formerly IFTU) is sponsored by the Iraqi Communist Party (ICP) and former U.S.-installed Prime Minister Iyad Allawi. These parties oppose immediate U.S. withdrawal, support Bush's "surge" and demand that the Iraqi puppet regime crush Iraqi resistance--positions that are echoed by the GFIW.<p>
Inexplicably, U.S. tour organizers deny these well-documented facts--see, for example, "Trojan Horse in the Antiwar Movement: Facts About the IFTU," from New York City Labor Against the War (NYCLAW), June 20, 2005.<p>
NYCLAW believes that such support for pro-occupation forces undermines genuine solidarity with Iraqi workers, weakens the fight for immediate U.S. withdrawal and has no place on an antiwar platform. We also believe that tour organizers owe an explanation for their continuing misrepresentation of GFIW's true position. <br>
<b>New York City Labor Against the War</b><p>
