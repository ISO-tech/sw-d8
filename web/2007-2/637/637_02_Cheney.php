<font face="Times New Roman, Times, serif" size="5"><b>The Constitution according to Cheney</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">July 6, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">TURNS OUT federal inmate no. 28301-016 won't spend even a single night in the clink.<p>
As <i>Socialist Worker</i> went to press, George Bush sparked yet more outrage by commuting the 30-month prison sentence of I. Lewis "Scooter" Libby, a former top aide to Dick Cheney, who was convicted of perjury for lying during the investigation into the politically motivated leak of the identity of CIA agent Valerie Plame.<p>
Bush claimed that Libby's sentence was "excessive"--that fines and the loss of his "stellar reputation" were enough of a punishment.<p>
More likely, Bush was concerned about what kind of squealing Scooter might do when faced with the inside of a prison cell--and doled out the commutation as payback for Libby taking the heat for Cheney, who by most accounts directed the Plame leak.<p>
The commutation of Libby's sentence was the latest in a series of new revelations that further highlight--if that's actually possible--the colossal arrogance and apparently criminal behavior of the vice president of the United States.<p>
It also turns out that Cheney avoided having to hand over classified documents to the National Archives by claiming--in defiance of everything you ever learned in government class--that he isn't part of the executive branch, and that his office isn't an "entity within the executive branch that comes into the possession of classified information."<p>
The claim that his office doesn't possess of classified information begs a question. Why, then, does Cheney's office contain a human-size safe, as the <i>Washington Post </i>recently reported.<p>
Maybe all his secret dirty dealings have made Cheney paranoid. The recent <i>Post</i> series on the vice president shows how Cheney twisted the law like a wet noodle to permit the torture of detainees, gut environmental regulations, slash taxes on the wealthy and business, and install his minions in powerful positions.<p>
Cheney may be just the most outrageous current example, but it should be remembered that America has a long traditions of presidential administrations willing to use dirty--frequently vicious and illegal--methods to pursue political aims.<p>
The release of the CIA's "family jewels"--a heavily censored archive of more than 700 pages of classified material--shows alliances with the Mafia to kill Fidel Castro; then-Attorney General Bobby Kennedy approving wiretaps on journalists; and the secret use of LSD on unsuspecting U.S. citizens, among other crimes. And these are just the things we know about.<p>
Under the surface, U.S. "democracy" isn't really so very democratic at all.<p>

