<font face="Times New Roman, Times, serif" size="3">He was an avid reader, studying the history and theory of working-class struggle from the Paris Commune of 1871 to the current struggles in Venezuela, and everything in between. He was always eager to explain his understanding of these events and ideas to anyone who would listen, but he did so with a patience and sincerity that won him the respect of even those who disagreed with him.<p>
Josh was the only person we knew who could explain the dynamics of the U.S. defeat in Vietnam while fixing your kitchen sink (and telling you all the ways in which your plumbing system was not up to code).<p>
Over the years of his too-short life, Josh stood with striking laundry workers in Washington, D.C., and led the D.C. branch in <a href="http://www.socialistworker.org/2006-1/572/572_02_WalMart.shtml" target="_blank">bringing the voices of these workers to the pages of <i>Socialist Worker</i></a>. He walked in solidarity with Verizon workers on their picket lines, and organized and marched in antiwar and abortion rights protests. Josh was a leading activist in Ralph Nader's 2000 and 2004 presidential campaigns, and he most recently demonstrated against outrageous electricity rate hikes in Baltimore.<p>
Josh never missed a picket against the death penalty outside Maryland's death row. He attended virtually every rally, town meeting and forum in the struggle to stop the executions of Vernon Evans, Wesley Baker and Steven Oken.<p>
Those of us who had the honor of knowing Josh will forever remember a man who put the needs of others before his own, and who wielded an incredible intellect without the slightest trace of pretentiousness. Josh was the best friend and comrade one could ever ask for. Joshua Brand--Presente!<p>

<font face="Times New Roman, Times, serif" size="4"><b>A passion for socialist politics</b></font><p>

<font face="Times New Roman, Times, serif" size="3">MY NAME is Jenni Rowe, and this is by far the most important document I will ever write. Josh Brand was my boyfriend for more than four years--and my informal fiance for some of that time.<p>
I was an ISO member for several years before reluctantly leaving due to my own emotional afflictions, and it was in this great organization that Josh and I met. Our personal relationship began in 2003 as we staffed a literature table at an anti-death penalty event at Howard University.<p>
Josh struck me as a man who was not only very intelligent and knowledgeable, but also very sweet and quirky in a wonderful way. He had an innocence about him which had nothing to do with naivete. As I spent more and more time with him, I began to feel that he was one of the best, most generous people I had ever met.<p>
Josh's passion for socialist politics was intense. He believed deeply in the possibility of another, better world born of working-class revolution. In defiance of his persistent mental and emotional symptoms, he dedicated his life to the advancement of that struggle.<p>
Over the past few years, Josh and I sometimes discussed our dream of co-authoring an <i>ISR</i> article with a Marxist perspective on mental illness (i.e., that capitalist society is both a cause and a beneficiary of it).<p>
Josh was my partner, my lover and my best friend. I am utterly devastated by his passing, and in this, I know that I am far from alone. His extremely tragic and untimely death is an enormous loss not only for myself and his many other loved ones, but also for the ISO, the Left, and the world as a whole.<p>

