<font face="Times New Roman, Times, serif" size="5"><b>In memory of Joshua Brand</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Ben Dalbey, Kevin James and Jennifer Rowe</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 6, 2007 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">WE MOURN the loss of our beloved friend and comrade, Joshua Brand, who tragically took his own life on June 24 in Baltimore at the age of 25. No words can adequately describe our sorrow at losing Josh. None can convey the intensity of our love, compassion and respect for this wonderful person. We will always remember him.<p>
Joshua Brand will always be that comrade whose dedication to justice and a better way of life will continue to inspire the rest of us. He will always be that friend whose generosity, sincerity and tenderness will remind us of the compassion at the heart of our politics as we fight for change.<p>
For Josh, struggle was not an abstraction. He was in his third year of an apprenticeship with the Plumbers and Steamfitters Local Union 486, and juggled 10-hour days, two-hour commutes, classes, union meetings and membership responsibilities in the ISO--all while wrestling with his own mental health issues and the impact of cancer in his immediate family.<p>

