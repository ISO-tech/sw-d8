<font face="Times New Roman, Times, serif" size="3">Liam was honorably discharged from the Marines last year and is now part of the Individual Ready Reserve (IRR), an inactive force whose members receive no pay or benefits and aren't obligated to drill, conduct annual training or participate in any military activities.<p>
The Marines had sought to give Liam an other-than-honorable discharge from the IRR, which would threaten his veterans' benefits. The Marines said they dropped the case because they had "received sufficient indication" from Liam that he wouldn't wear his uniform at future political events.<p>
But Liam said he planned to keep wearing his uniform. He had offered to stop wearing his uniform at protests if the Marines agreed to put in writing that "my statements are neither disloyal nor inaccurate," but since he didn't receive any such communication, he believes there is no agreement.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">EVEN BEFORE Liam got word that the Marines were backing down, he and several other IVAW members had begun an IVAW "Grill out, chill out" bus tour to reach out to active-duty troops at nine bases throughout the eastern U.S. between June 23 and July 8.<p>
At each of the stops (for a full list, go to <a href="http://www.ivaw.org/bustour" target="_blank">www.ivaw.org/bustour</a>), IVAW members handed out leaflets to soldiers at shopping areas and hangouts, held barbeques and called press conferences--and they attended the U.S. Social Forum in Atlanta on June 30.<p>
At some bases, the military tried to keep the IVAW from meeting with troops. At Fort Jackson in Columbia, S.C., five IVAWers were detained, escorted off base and released without charges.<p>
Two days later, at Fort Benning, two IVAW members--Liam and Nathan Lewis--were arrested and charged with criminal trespassing, for simply trying to go on to the base to meet with soldiers while wearing "Iraq Veterans Against the War" T-shirts. Adam Kokesh changed out of his IVAW T-shirt to avoid arrest, but when he approached the gate, he was also immediately detained.<p>
The bus tour ends at Fort Drum in Watertown, N.Y., on July 8, the same day that the Different Drummer Caf&eacute;--a GI coffeehouse started by the New York City-based Citizen Soldier--is holding a symposium on returning soldiers and post-traumatic stress disorder.<p>
The event was timed to coincide with the beginning of a court-martial proceeding against Spc. Eugene Cherry, who went AWOL in November 2005 to live with his family in Chicago and seek mental health counseling that he sought but didn't receive at Fort Drum.<p>
After a year, Eugene returned to Fort Drum to resolve his status with the military, which recently informed him that it would hold a court-martial against him on July 9.<p>
But as <i>Socialist Worker </i>went to press, the military informed Eugene that it would halt the court-martial--which could have saddled Eugene with a dishonorable discharge and time in a military brig--and instead offer him an administrative other-than-honorable discharge.<p>
"I consider this a victory," explained Citizen Soldier Director Tod Ensign, who explained that such a discharge won't bar Eugene from receiving service-connected treatment through the Veterans Administration.<p>

