<font face="Times New Roman, Times, serif" size="5"><b>Soldier in Iraq takes a stand against war</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 6, 2007 | Pages 1 and 7</font><p>

<font face="Times New Roman, Times, serif" size="3">THE THREE-month period ending June 30, in which 329 soldiers were killed, was the deadliest for U.S. troops since the war on Iraq began--and also a time when antiwar soldiers undertook some bold initiatives and won victories.<p>
Army Spc. Eleonai "Eli" Israel was stationed at Camp Victory in Baghdad when he told his commanding officers June 19 that he would no longer participate in the illegal and unjust U.S. war on Iraq. "We are now violating the people of this country in ways that we would never accept on our own soil," said Eli.<p>
In communications sent to his friends, Eli wrote, "I could spend the rest of my time here (shooting, getting shot at, and watching my friends get killed and maimed) 'wondering' why the Bush/Cheney administration lied to me. But I really don't think that I have to understand in full what their interests were in order to understand that they were not my interests, either as a soldier or as a civilian."<p>
Meanwhile, the U.S. Marine Corps announced it was dropping its push to sanction Iraq Veterans Against the War (IVAW) member Liam Madden for making antiwar statements and wearing his uniform at a protest.<p>
"I think it's a total victory," said Liam, who had been targeted by the Marines for calling the invasion of Iraq a "war crime" and saying that Bush had betrayed the troops.<p>

