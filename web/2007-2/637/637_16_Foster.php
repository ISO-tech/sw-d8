<font face="Times New Roman, Times, serif" size="5"><b>Facing execution for driving a car</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Bryan McCann</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 6, 2007 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">THE STATE of Texas plans to execute Kenneth Foster Jr. August 30 for the 1996 murder of Michael LaHood Jr.<p>
What makes Foster's case unique is that he killed no one--and the state of Texas is first to admit this.<p>
How is this possible? Texas' Law of Parties, adopted in 1974, allows prosecutors to hold all those present legally responsible for a crime. Because Foster was driving the car carrying Mauriceo Brown the night Brown shot LaHood, prosecutors were able to try Kenneth as if he was the shooter.<p>
Brown, who was executed in July 2006, admitted to shooting LaHood, but claimed it was in self-defense. He also insisted that Foster, who remained in a car 80 feet away from the shooting with the radio on and windows rolled up, didn't know he had left the car with the gun.<p>

