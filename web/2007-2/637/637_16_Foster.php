<font face="Times New Roman, Times, serif" size="3">In addition to being railroaded onto death row by the Law of Parties, Kenneth is a founding member of the Death Row Inner-Communalist Vanguard Engagement (DRIVE), a group of brave death row inmates who organize protests for abolition and better living conditions on Texas's death row.<p>
As Kenneth says, "We are neither violent nor passive. We are combative. We are resisters. We are diverse activists, but more than anything else, may we be looked upon as men who embraced the sacredness of life and sought to assert the full measure of their humanity in the face of those that would seek to destroy it."<p>
Last week, Kenneth's criminal lawyer, Keith Hampton, submitted a new appeal to the Texas Court of Criminal Appeals. If the court refuses to grant Kenneth relief, his supporters will then turn to the Board of Pardons and Paroles and Gov. Rick Perry for clemency.<p>
After Kenneth's execution date was announced in May, a broad coalition called the Save Kenneth Foster Campaign was formed. The Austin and Corpus Christi chapters of Campaign to End the Death Penalty (CEDP), along with Kenneth's family and lawyers, as well as other anti-death penalty groups, have been holding weekly meetings to build a movement around this case.<p>
The coalition held a petition and literature table at Austin's Juneteenth festival on June 19. This annual commemoration of emancipation from slavery provided an excellent opportunity to reach out to the community. The coalition got over 200 signatures on the petition to the Court of Criminal Appeals.<p>
Texas is on track to perform its 400th execution since 1982 this summer. The case of Kenneth Foster Jr.--a Black man sent to death row for driving a car--is a testament to how rotten Texas's machinery of death truly is.<p>

