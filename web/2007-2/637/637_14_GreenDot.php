<font face="Times New Roman, Times, serif" size="5"><b>UFT courts charter school boss</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">July 6, 2007 | Page 14</font><p>

<font face="Times New Roman, Times, serif" size="3">MEGAN BEHRENT of the United Federation of Teachers in New York City and RANDY CHILDS of United Teachers Los Angeles in LA report on the Green Dot charter scam.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">APPARENTLY NOT content with merely betraying the interests of the over 100,000 New York City teachers and education staff in her own union, United Federation of Teachers (UFT) President Randi Weingarten reached across the country to stab the 48,000 members of United Teachers Los Angeles (UTLA) in the back as well.<p>
On June 28, the UFT announced a "partnership" with Green Dot Public Schools--the most prominent operator of charter schools in Los Angeles--to open a charter high school in the South Bronx. This deal comes on the heels of Weingarten's secretive trip to Los Angeles to visit Green Dot schools.<p>
Green Dot has ingratiated itself with powerful UTLA opponents like local billionaire Eli Broad by promoting a "reform" model which guts teachers' workplace rights, seniority, and health and retirement benefits.<p>
Weingarten's move undermines UTLA's efforts to defend its hard-fought contractual rights against the menace of Green Dot and other charter operators, and it represents a setback for New York City teachers fighting for better work conditions and more funding for New York City's public schools.<p>
Weingarten's courtship with Green Dot was greeted with euphoria by the UTLA-bashers at the <i>Los Angeles Times</i>, who urged Los Angeles Unified School District officials to "take on" the "regressive" UTLA "dinosaur" and "push UTLA in the same direction" as Weingarten's UFT.<p>
Indeed, comparing the directions of UTLA and UFT is instructive. Last year, UTLA rode a 10,000-member rally and a strike authorization vote to a contract victory that gained a 6 percent raise, won hundreds of millions of dollars for class-size reduction and made no concessions on work rules or seniority.<p>
The UFT cancelled a May community rally of teachers, parents and students to accept a rotten "restructuring" deal with Mayor Michael Bloomberg that will give school administrators an incentive to discriminate against experienced teachers and open the door to privatization and future attacks on tenure.<p>
The ugly truth is that charter schools like Green Dot represent the 21st century version of "tracking," the practice of identifying the "best" students in public schools, giving them better learning conditions and a college-prep curriculum while leaving the vast majority of students behind.<p>
So when Randi Weingarten gushes that Green Dot schools "vastly outperform comparable traditional public schools," she's helping the enemies of teachers' unions and public education sweep the issues of underfunding and overcrowding under the rug.<p>
The union is promoting a "solution" that viciously attacks teachers' rights and makes things worse for the vast majority of public school students. In fact, the UFT has opened two charter schools itself in Brooklyn.<p>
Rather than giving ground to union-busting charter schools who cherry pick students and weed them out if they don't bolster their statistics, our unions should be fighting for more funding for public education for all students, and for better working conditions for teachers to provide children and teens with the education they deserve.<p>

