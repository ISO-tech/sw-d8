<font face="Times New Roman, Times, serif" size="5"><b>Thousands flock to U.S. Social Forum</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">July 6, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>ASHLEY SMITH reports from Atlanta about the first U.S. Social Forum.</b><p>

ATLANTA--More than 10,000 activists mobilized from across the U.S. to attend the first ever U.S. Social Forum here from June 27 to July 1.<p>
They were young and old, multiracial, men and women, gay and straight, immigrant and native, and overwhelmingly working class. They came with community groups, non-governmental organizations (NGOs), unions and political formations.<p>
The activists filled the Atlanta Civic Center, hotels, local churches and other meeting places for vibrant discussions about politics and organizing around major themes of immigrant rights, U.S. imperialism, reconstruction of the Gulf Coast after Katrina, indigenous struggles, women's rights, sexual oppression and workers' struggles.<p>
More than 900 workshops addressed the sundry ways that corporate globalization and neoliberalism have negatively impacted the lives of workers and the oppressed. But this was not an academic conference--organizers shared ideas and strategies for building movements for social change.<p>
Activists came in search of radical ideas to explain and change U.S. society. They flocked to literature tables, and hundreds sought out meetings that discussed challenging conservative NGOs or debated the building of revolutionary parties.<p>
At one plenary, the veteran Latina activist and socialist Betita Martinez called for ending capitalism and declared that we should all be, quoting Angela Davis, "communists with a small 'c.'" Close to 2,000 people in the audience gave her a standing ovation.<p>
Amid the kaleidoscope of issues, the movements against U.S. imperialism and for immigrant rights stood out in particular.<p>
In one plenary on militarism, Faleh Abood Umara, of the Iraqi Federation of Oil Workers, received thunderous applause when he declared that his union would fight against the neocolonial oil law pushed by Washington, and for workers' rights and an end to the occupation.<p>
Numerous workshops, including two held by the Campus Antiwar Network (CAN), took up the issues related to the occupation. "The forum was a great opportunity for organizing students who are looking to build antiwar coalitions and in particular build student solidarity with Iraq Veteran Against the War," said CAN activist Chris Dols.<p>
The meetings on immigrant rights organizing conveyed the state of emergency in the wake of countless raids and deportations. New York City activist Ariella Cohen said that at her workshop, grassroots activists shared "ideas for resisting raids by building emergency response networks. These networks have the potential to lay the foundation for a grassroots and militant alternative to defend immigrants and demand full civil rights."<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">THE FORUM was an enormous step forward, but it was not without challenges and problems. Overall, the movements are just beginning to knit themselves together on particular issues, let alone develop into a movement of movements.<p>
And there continue to be political weaknesses that hamper this development. In particular, some activists are still prone to exclude or distort the issue of Palestine.<p>
For example, during the plenary session on U.S. imperialism, one speaker equated the Palestinian Islamist group Hamas with George Bush, and received applause. This prompted Palestinian activists to demand a speaker who rebutted this preposterous equation of an imperialist power and an oppressed people's resistance.<p>
Speakers at the large plenaries also tended to describe new struggles in the language of identity politics--the idea that oppressed groups have separate and distinct struggles. This was at odds with an evident desire to link struggles together into a common one.<p>
Nevertheless, as Ruben Solis from the Southwest Workers Union stated, "The social forum has been tremendously successful. It has opened a new stage, a new paradigm, of organizing in the U.S., based on solidarity that must continue after the forum."<p>
Solis called particular attention to how the forum helped "broaden the conception of the immigrant rights struggle as more than just a fight for legal rights, but one for worker's rights and fundamental change in U.S. society."<p>
Anti-apartheid activist and veteran of the World Social Forum movement Dennis Brutus said the U.S. Social Forum had been "important in two ways. First, if it puts forward a radical message of systemic change, it can prove to the people of the world that they have allies in the U.S. that they can struggle shoulder to shoulder with. Second, the USSF also, through its radical message, can possibly reinvigorate the World Social Forum itself, which has lost its radicalism and sense of purpose.<p>
"So the forum offers tremendous hope, not only for the U.S. but the world movement against corporate power."<p>
One moderator declared at the concluding People's Assembly, "Many questioned having a social forum in the U.S., because all the U.S. does is bomb people.<p>
"But maybe there's a difference between the government and the people. Maybe people have more in common with the rest of the world's people than with their own government. Maybe the people can change the U.S. and its government. Maybe another U.S. is thus not only possible, but necessary."<p>

