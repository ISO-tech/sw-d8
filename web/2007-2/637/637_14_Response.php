<font face="Times New Roman, Times, serif" size="5"><b>Organizing a response to the raids</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Raquel Vega</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 6, 2007 | Page 14</font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--Some 60 people attended a workshop June 30 to learn how to respond to immigration raids and deportations, and discuss how to further the immigrant rights struggle.<p>
The workshop was the second event put on by the newly formed Emergency Response Network (ERN) against raids and deportations. It was held in the Pilsen neighborhood, the heart of Chicago's Mexican-American community.<p>
Speakers focused on key issues for organizers. A representative from the Mexican Consulate in Chicago explained which services the consulate provides to immigrant detainees, while a speaker from the Latino Union provided an interactive know-your-rights presentation.<p>
Another speaker--a man whose pregnant wife was deported--gave an account of the struggle to return her to the U.S. that spoke to the growing numbers of families separated by stepped-up raids.<p>
The Chicago ERN was inspired by the immediate response to a raid that happened in Chicago just before May Day, when hundreds of protesters took to the streets to oppose the raid and lockdown on a strip mall in the largely Mexican Little Village neighborhood.<p>
Last month, the Chicago ERN took a busload of people to a town hall meeting in the suburb of Carpentersville to protest a proposed English-only local ordinance. The network's future actions might include going to the nearby suburb of Waukegan, where local officials are considering implementing a "polimigra" policy--that is, giving local police authority to enforce federal immigration laws.<p>
The ERN began as a small core of about eight people, who made contacts and promoted the workshop widely. Through a press release and utilizing some contact information, members were able to get out information through the radio, television interviews and newspaper articles.<p>
This resulted in a flood of calls from interested people. But what was most important in launching the network was posting flyers and calling people who had signed up to be part of the network.<p>
The Chicago ERN is one of several being formed around the U.S. and is a sign that the immigrant rights movement will not let the government's crackdown go unchallenged.<p>

