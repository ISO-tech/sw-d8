<font face="Times New Roman, Times, serif" size="5"><b>Judge keeps Al-Arian behind bars</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 6, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">A FEDERAL judge has decided to continue the imprisonment of former University of South Florida professor and Palestinian rights supporter Sami Al-Arian on trumped-up contempt charges.<p>
Al-Arian has been imprisoned since 2003, when he was accused by the government of using an academic think tank and Muslim charity and school to "materially aid" terrorism. In 2005, a jury voted to acquit or deadlocked on every charge against Al-Arian. But the government refused to release Al-Arian.<p>
He later took a plea deal on a single count of the least serious charge against him in exchange for what was supposed to be a short additional sentence, to be followed by voluntary deportation. The Feds reneged on the deal, saying that Al-Arian should be compelled to testify in another "terrorism-related" case in Virginia, even though his plea agreement specifically exempted him from this.<p>
For his refusal to testify, Al-Arian last year was found to be in contempt of court, and given an 18-month sentence--which the government vowed it would drop only when he agreed to testify.<p>
In a closed hearing last month, U.S. District Court Judge Gerald Lee upheld the contempt sentence and ordered Al-Arian to be held until at least October.<p>

