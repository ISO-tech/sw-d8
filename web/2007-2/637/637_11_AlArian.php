<font face="Times New Roman, Times, serif" size="3">According to the Tampa Bay Coalition for Justice and Peace (TBCJP), which organizes support for Al-Arian, Lee rejected the government's argument that Al-Arian must automatically spend the full 18 months in jail. Rather than order his release, however, Lee ruled that given Al-Arian's strong ties to his family, the contempt sentence may still coerce him to speak in order to be reunited with them.<p>
"In fact, Dr. Al-Arian's desire to rejoin his family is exactly the reason he will not testify, because he believes doing so is a road to entrapment, future prosecutions and many more years of unjust incarceration," the TBCJP said in a statement.<p>
Lee did agree, however, that if he finds further incarceration won't compel testimony, Al-Arian should be set free from civil contempt.<p>
In the meantime, the suffering of Al-Arian and his family continues. During his time in prison, Al-Arian has been forced to endure unsanitary conditions, lack of proper medical care, and racist verbal and physical abuse at the hands of prison guards who call him a "terrorist."<p>
During a 60-day hunger strike earlier this year to protest his indefinite detention, Al-Arian received little medical attention, even as his health rapidly deteriorated. In April, a guard reportedly told Al-Arian that he'd like to "put a bullet in [his] head and get it done with."<p>
Meanwhile, because of the hardship of the trial and its aftermath, Al-Arian's wife Nahla is now planning to take their two youngest children and move to Egypt--even as Al-Arian remains in prison.<p>
As defense attorney Jonathan Turley said, "Dr. Al-Arian has now been imprisoned for five years despite the fact that he was never convicted of any count in the Florida trial. He has been confined for six months and two grand juries. It was always obvious that he would not yield to these coercive measures and that the real purpose of the government is to punish a man who they could not convict in a court of law."<p>

