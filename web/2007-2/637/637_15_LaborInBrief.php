<p><a name="Iraqi"></a><font face="Times New Roman, Times, serif" size="4"><b>Iraqi labor tour</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><p>

<font face="Times New Roman, Times, serif" size="3">LEADERS OF Iraqi trade unionists toured the U.S. in June to give Americans insight into the class struggle in Iraq and foster solidarity for their cause.<p>
The 12-city tour, organized by U.S. Labor Against the War (USLAW), showed an interest by U.S. labor activists in building solidarity with Iraqi workers.<p>
However, as was the case in a similar tour in 2005, the events were clouded by controversy over the pro-occupation position of one of the union federations represented by one speaker--Hashmeya Muhsin Hussein, president of the Electrical Utility Workers Union, which is affiliated with the General Federation of Iraqi Workers (GFIW).<p>
As a statement on the tour by New York City Labor Against the War (NYCLAW) put it, "The GFIW (formerly IFTU) is sponsored by the Iraqi Communist Party (ICP) and former U.S.-installed Prime Minister Iyad Allawi. These parties oppose immediate U.S. withdrawal, support Bush's 'surge,' and demand that the Iraqi puppet regime crush Iraqi resistance--positions that are echoed by the GFIW."<p>
Abdullah Muhsin, international representative of the then-IFTU, was quoted in January as saying that "If the American troops leave, Iraq will become a bloodbath and turn toward the dark ages." Similarly, Wishyaar Hamad Haji, a top official in the Kurdistan Teachers' Union, an affiliate of the GFIW, said on a tour of Britain earlier this year that a troop withdrawal would be a "catastrophe."<p>
"NYCLAW believes that such support for pro-occupation forces undermines genuine solidarity with Iraqi workers, weakens the fight for immediate U.S. withdrawal, and has no place on an antiwar platform," the group said in its statement.<p>
The USLAW tour did include Faleh Abood Umara, General Secretary of the Iraqi Federation of Oil Workers, which opposes the occupation position and which recently went on strike in defiance of the government.<p>
However, visa difficulties prevented Umara from reaching Washington, D.C., for the opening of the tour, which included a protest at the office of BearingPoint, a consulting company that wrote the proposed Iraq oil law that would virtually privatize 70 percent of the company's oil wealth.<p>
<i>Jon Van Camp contributed to this report</i>.<p>

