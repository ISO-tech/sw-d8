<a name="Delphi"></a><font face="Times New Roman, Times, serif" size="4"><b>Delphi</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WORKERS AT bankrupt Delphi Corp. last month approved a contract that cuts wages and closes plants as United Auto Workers (UAW) leaders pushed for some of the most drastic concessions in the union's history.<p>
The deal, approved by a 2-1 margin, reduces top pay from about $27 per hour to $18.50 and clears the way for the company, the former parts division of General Motors, to close 10 U.S. plants, sell four others and keep four open as the company transforms into a largely offshore supplier for GM and other auto companies.<p>
The vote comes nearly 18 months after Delphi declared bankruptcy. Since then, Delphi and GM funded buyouts for approximately 20,000 Delphi workers who took earlier retirement or transferred back to GM.<p>
As a result, of the 17,000 current Delphi workers eligible to vote on the deal, just 4,000 were UAW veterans. The rest were new hires who were already paid on a lower tier of $14 per hour, and had no opportunity to earn the top rates anyway.<p>
For many of these new hires, the contract was in fact a vote on a severance package they will receive when their plants shut down.<p>
More givebacks will soon be on the table at local plant contracts to be completed over the next two months. The UAW has agreed that the local agreements will reduce skilled trades classifications and eliminate restrictions on combinations of jobs.<p>
"The UAW is now neck and neck with the unorganized in a race to the bottom," wrote Gregg Shotwell, a former Delphi worker who is employed at a GM warehouse in Michigan. "In an effort to pull ahead the [UAW Administration] Con Caucus shed the burdensome seniority rights sometimes referred to as 'work rules.' The Con Caucus sees no reason why one person can't do two jobs. After all, Con Caucus reps can talk out of both sides of their mouths at the same time."<p>
The deal has ominous implications for the Big Three contract talks that will soon officially begin. The UAW has effectively agreed to reopen the current contract by taking billions of dollars in concessions on health care costs at GM and Ford and buyouts to eliminate jobs. Local contracts at Ford and GM have also been renegotiated to give management greater "flexibility."<p>
If employers have their way this year's negotiations will further roll back UAW gains built up over decades.<p>
