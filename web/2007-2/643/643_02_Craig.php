<font face="Times New Roman, Times, serif" size="5"><b>The real crimes of Larry Craig</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 7, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">LAST WEEK, another Republican member of Congress, Idaho Sen. Larry Craig, found himself in the eye of a scandal, forced to resign after it was revealed that he had pled guilty to disorderly conduct after allegedly trying to solicit sex in a Minneapolis-St. Paul airport men's bathroom in June.<p>
But before he became known as the guy who tried to get out of being arrested by passing his Senate business card to a police officer and asking, "What do you think about that?" Craig was better known an arch-conservative who led the charge against Bill Clinton after it was revealed he'd had sex with a White House intern.<p>
Craig was part of the congressional Republicans' inner circle, a member of the "Singing Senators" barbershop quartet that included civil-liberties-shredding holy roller John Ashcroft and Mississippi segregationist Trent Lott.<p>
He made a career out of scapegoating the most vulnerable people in society--in particular, going after undocumented immigrants and gays and lesbians--and could be counted on to support every punitive conservative cause, such as extending the USA PATRIOT Act, that his friends in the White House came up with.<p>
A month after Hurricane Katrina decimated the Gulf Coast in 2005, Craig proposed abandoning the flooded parts of New Orleans. "I'm not humorous when I suggest we should turn it back to what it was, a wetland," he said of the Lower Ninth Ward.<p>
But the week of uproar among Republicans and in the media exposed more than Craig's rank hypocrisy. It also spoke volumes about what passes for resignable offenses in Washington politics.<p>
As the footage of Craig's resignation announcement repeated endlessly in the cable TV news echo chamber--"I am not gay, I have never been gay," Craig declared--it was an infuriating commentary on what is considered a "crime" in U.S. society. Craig's arrest was second-rate entrapment for something that shouldn't be against the law in the first place.<p>
The real crime is the antigay atmosphere in the U.S., cultivated by people like Craig, who vilify gays and lesbians by banning same-sex marriage, keeping gay bashing off the federal hate crimes list, and enforcing the "don't ask, don't tell" policy in the military. Their actions have reinforced a climate where people are afraid to express their sexuality because they might lose their jobs--or even their lives.<p>
The blame for that doesn't just stop with Larry Craig, of course. It also belongs with his Republican friends like Trent Lott and Mitch McConnell, who forced him to quit, and with all the rest of the upstanding citizens in Congress.<p>

