<p><a name="Tijuana"></a><font face="Times New Roman, Times, serif" size="4"><b>Tijauna teachers' border protest</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Norma Villegas and Rick Greenblatt</b></font><p>

<font face="Times New Roman, Times, serif" size="3">TIJUANA--Thousands of teachers shut down the Tijuana/San Diego border crossings at San Ysidro and Otay Mesa August 31 to protest pension cutbacks and oppose their unpopular union leadership.<p>
As many as 7,000 militants from Tijuana, Ensenada, Rosar&iacute;to, San Quint&iacute;n and other Baja California cities left their classrooms for the day and marched to the San Ysidro border crossing, stopping traffic with a sit-in that lasted for more than two-and-a-half hours and shutting the U.S.'s largest land port of entry.<p>
The Tijuana demonstration was one of several held by teachers across Mexico's Northern and Southern borders that day, involving members of Mexico's national teachers union (SNTE, from its Spanish initials).<p>
Carrying home-made signs opposing the new pension law, the teachers marched from an early morning rally to the border crossing, where they were stopped by hundreds of municipal and state police, many clad in riot gear and carrying automatic weapons. The orderly and disciplined marchers then sat down, blocking 15 lanes of traffic, chanting, singing and listening to speeches by union militants.<p>
The new law, part of Mexican President Felipe Calder&oacute;n's neoliberal economic program, requires teachers and other government workers to make larger contributions to their pension funds and work longer to receive benefits. The law is being challenged by teachers in Mexico's Supreme Court.<p>
Tension has been building in Baja since teacher occupations of their SNTE union offices across northern Baja, begun May 17, were broken up by state police August 26. Two teachers were injured in the raid.<p>
According to teachers at the protest<i>,</i> SNTE Local 2 President Arcelia Galarza backed the police attack, and union militants are calling for her removal from office. The teachers are also demanding the resignation of Elba Esther Gordillo, the SNTE president, for supporting Calder&oacute;n's new pension law.<p>

