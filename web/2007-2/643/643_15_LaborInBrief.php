<a name="Smithfield"></a><font face="Times New Roman, Times, serif" size="4"><b>Smithfield Foods</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WILLIAMSBURG, Va.--Backed by more than 1,000 supporters, workers from Tar Heel, N.C., presented an August 29 shareholders meeting of Smithfield Foods with petitions signed by thousands of workers demanding the right to a union.<p>
About 150 workers traveled to Williamsburg to present their demands, joined by members of unions, faith-based groups, community organizations, and civil and immigrants rights activists from around the country.<p>
A majority of the Smithfield workers in the Tar Heel pork-processing plant, which employs some 5,000 people, signed one petition. Another was signed by supporters, as well as Smithfield workers from all over the world, including Iowa, Nebraska, and even Spain, Poland and France.<p>
After a delegation of Tar Heel workers presented their demand at the shareholders' meeting, they met supporters--in a sea of yellow "Justice at Smithfield" T-shirts--for a rally nearby. "You see all of these people out here," Vincent Nash, a Tar Heel worker, told WAVY news. "We've got a lot of support, and the time is coming for there to be a change."<p>
The United Food and Commercial Workers (UFCW), which has sought to represent the workers at Tar Heel for more than a decade, wants Smithfield to agree to card check--that is, recognizing the union after a majority of workers sign union membership cards. But management wants a secret ballot under the auspices of the National Labor Relations Board (NLRB).<p>
The UFCW lost two previous attempts to represent workers at Tar Heel--in 1994 and 1997--after the company rigged NLRB-run elections. An NLRB administrative law judge ruled in 2000 that the 1997 vote was invalid, and that management had to rerun the vote. The company appealed in federal court, but in May 2006, a judge upheld the union's complaint.<p>
The UFCW is concerned that management will again intimidate workers into voting against union representation. To that end, the company has tried to pit white against Black and Black against Latino at every step of the way.<p>
 "The card check gives us a little protection knowing that the company cannot interfere with the election. But they didn't want to hear that," Terry Slaughter, a Smithfield worker who addressed the shareholders, told the <i>Fayetteville Observer</i>.<p>
During the bitter fight at Smithfield--one of the most dangerous places to work in the country--the company has used every weapon in its arsenal to keep the union out, such as firing pro-union workers and arranging the arrest and deportation of immigrant workers.<p>
But workers have carried out a determined struggle, including organizing walkouts over unsafe drinking water and other issues. They've also visited cities across the U.S. to build support.<p>
"If we allow Smithfield to trample on the rights of workers at Tar Heel, then you better believe other companies will line up and follow in Smithfield's steps," MaryBe McMillan, a representative with the North Carolina AFL-CIO in Raleigh, said at a rally before the shareholders' meeting. "The labor movement cannot and will not allow Smithfield or any other company to intimidate workers and deny them their fundamental right to organize."<p>
