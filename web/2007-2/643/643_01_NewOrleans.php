<font face="Times New Roman, Times, serif" size="5"><b>National Guard descends on N.O. protest</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alden Eagle and Gimena Gordillo</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 7, 2007 | Pages 1 and 8</font><p>

<font face="Times New Roman, Times, serif" size="3">THE NATIONAL Guard on the streets of New Orleans. It's an image that recalls the pleas for help from flood victims in the aftermath of Hurricane Katrina two years ago.<p>
But the Guard was in short supply then--since so many of its members had been deployed to Iraq. When they did arrive, their orders were to <i>police </i>the people battered by Katrina, not help them.<p>
Two years after Katrina, the National Guard was blockading streets in New Orleans--but in response to protesters demanding that needed public housing in New Orleans be reopened.<p>
On August 31, National Guard soldiers and New Orleans police sealed off the streets around the Gentilly offices of the Housing Authority of New Orleans (HANO)--in response to a sit-in of 20 peaceful protesters.<p>
"The police presence was excessive, and it was disproportionate considering the presence of peaceful folks who were in the building," said Rosana Cruz, co-director of the New Orleans group Safe Streets. "When I arrived, I counted 14 vehicles and Humvees from the New Orleans Police Department and from military police. Then, a SWAT team arrived, and the police presence increased even more."<p>
Many New Orleans residents left behind after Katrina languished without food and water for days, but it took no time two years later for the Guard to descend on people asking to return to their homes. Rather than listen to the residents' grievances, HANO shut down for the day.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">NEW ORLEANS has 5,000 empty public housing units, many of which had only minor damage from the storm. Those who used to live in these units must squeeze in with relatives or live in the streets.<p>
Instead of reopening public housing, HANO wants to demolish the four largest developments. Their plans call for new "mixed income" neighborhoods--meaning neighborhoods that are too expensive for the people who used to call them home.<p>
HANO's plan also calls for one of the developments to be replaced by two "championship" golf courses.<p>
New Orleans is at about two-thirds of its pre-Katrina population. The city government is encouraging people displaced by the hurricane to come home, but the lack of housing and services makes that impossible for many.<p>
The city's one public hospital, Charity, was closed illegally after the storm, so the poor and uninsured have literally no health care options. Some have been forced to travel to Baton Rouge, an hour-and-a-half drive, for routine care. There is no public care for mental health, despite the surge in people suffering from depression and post-traumatic stress.<p>
Only one-sixth of city buses and one streetcar line are back in service, meaning that it is nearly impossible to get around town without a car.<p>
The majority of schools are still closed, and those that reopened were mostly turned into charter schools. One lone school has reopened in the Lower Ninth. As a result, many parents trying to rebuild their lives in New Orleans feel like they need to leave their children in the care of relatives elsewhere. The school department has been scrambling to staff the few open schools, but this is because teachers were fired after the storm.<p>
Bush marked the two-year anniversary of Katrina last week and talked about the city's "progress." He spoke only a few blocks away from the hardest-hit areas of the Lower Ninth--those closest to the breached Industrial Canal.<p>
Almost nothing is left on many blocks. Most lots are abandoned, choked with weeds, with no sign that a house was once there except for the remaining concrete front steps. In other spots, where residents are still fighting to rebuild, the grass is carefully trimmed, but still, the lot is empty.<p>
In addition to the many residents fighting to return to closed public housing, there are thousands more trying to get back inside the houses they own.<p>
The city has made it easy for developers to get a property declared "blighted" and then swoop in to purchase it on the cheap, with no say whatsoever from its owner. So throughout the city, there are empty houses where owners have spray-painted a plea: "Don't demolish!"<p>
"Promises, that's all there is, promises," 58-year-old Sharon Sears Jasper, who lived at the St. Bernard complex in the Seventh Ward off and on since she was baby, told the New Orleans <i>Times-Picayune. </i>"I want to be home. That's my house."<p>

