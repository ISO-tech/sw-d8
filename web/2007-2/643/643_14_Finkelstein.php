<font face="Times New Roman, Times, serif" size="5"><b>Fighting for his right to teach</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 7, 2007 | Page 14</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>KATHRYN WEBER reports on protests by students and faculty in support of Norman Finkelstein.</b><p>

CHICAGO--Professor Norman Finkelstein is continuing his fight after the DePaul University administration denied his application for tenure in June and then cancelled the three courses he was scheduled to teach in his "terminal year," starting this fall<p>
Finkelstein is a tireless critic of Israeli policy toward the Palestinian people, and pro-Israel ideologues like Alan Dershowitz publicly campaigned to pressure administrators. Despite the support of students and colleagues, university officials announced at the end of the last school year in June that they would deny Finkelstein tenure.<p>
Now at the end of August, less than two weeks before his first class was to begin, the university announced that Finkelstein's three courses in the fall quarter--"Freedom and Empowerment," "Equality and Social Justice" and "States, Markets and Societies"--had been cancelled, and that he had been placed on administrative leave for the year.<p>
The American Association of University Professors (AAUP), already critical of the tenure denial, condemned this latest decision as a violation of "the standards of the AAUP, and those of DePaul's own faculty handbook."<p>
Finkelstein's student supporters continued to meet throughout the summer, and at the university's convocation on August 30, they invited faculty to join them in wearing shirts that read, "We are all Professor Finkelstein"--echoing the Palestinian freedom cry, "We are all Palestinians."<p>
Students quickly ran out of the 50 shirts they had made. About half went to faculty, to wear with their robes, and half to community members who showed up to demonstrate their support for Finkelstein.<p>
Several organizations were represented, including Jewish Voice for Peace, the Chicago Coalition Against War and Racism, Not in My Name, Committee for a Just Peace in Israel and Palestine and a local Presbyterian church. DePaul student organizations included the Academic Freedom Committee, Students for Justice in Palestine, DePaul Democrats, DePaul Students Against the War and the International Socialist Organization.<p>
In addition to the shirts, protesters held signs that read, "Norman Finkelstein: Good for DePaul" and "Norman Finkelstein: Favorite Teacher." At the convocation itself, students stood in protest during Provost Helmut Epp's speech. During the announcement of tenure recipients, students held a banner saying "Tenure for Finkelstein" and walked out silently.<p>
As <i>Socialist Worker </i>went to press, student supporters of Finkelstein were planning for the first day of classes. Finkelstein plans to teach the classes that were scheduled for Wednesday morning, and his students will be there to learn. In the end, it's up to the department and university faculty to determine how a student earns credits and fulfills requirements.<p>
Following the classes, students will accompany Finkelstein to the political science department to support his demand that his office be opened to him. They are willing to be arrested at his side to show their solidarity. Even more community organizations are expected to be present.<p>
The demands of the students have expanded beyond requiring that Finkelstein and Mehrene Larudee--another DePaul professor denied tenure for her support of Finkelstein, as well as her own position on the Israel-Palestine conflict--be granted tenure and the last stage of the tenure process investigated. They are now demanding the resignation of the man responsible for allowing this abuse to continue, DePaul President Father Dennis Holtschneider.<p>
