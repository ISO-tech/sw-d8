<font face="Times New Roman, Times, serif" size="5"><b>Furor over Chicago teachers contract</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jesse Sharkey, CTU delegate</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 7, 2007 | Page 14</font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--A delegates' meeting of the Chicago Teachers Union (CTU) erupted in anger August 31 after CTU President Marilyn Stewart rammed through a vote to approve a tentative contract.<p>
After Stewart called for a division of the house in order to vote on the agreement, those voting "yes" stood up. A few minutes later--with no time for a head count--Stewart ruled that the deal had been approved and refused to even call for "no" voters to stand.<p>
Delegates were furious. Many shouted for "roll call," while others stood on chairs, shaking their fists at the podium. Angry members stormed downstairs to a press conference that Stewart had called and took it over, chanting and stomping on the tentative agreement and even setting it on fire.<p>
During the stormy three-hour meeting, some 800 delegates representing 600 schools and citywide positions criticized and loudly booed the deal for its length (five years) and failure to win a daily prep period for elementary teachers. The proposed contract also generated heated discussion on health care premiums, which would be frozen for only three years in a five-year deal.<p>
The proposed deal is probably the best in several recent contracts in terms of raises (4 percent a year, and three extra steps in pay) and job security provisions. But it fails to address teachers' growing frustration with working conditions.<p>
For example, several delegates spoke about being under pressure to raise test scores and frustrated by oversized classes. The contract allocates just an extra $50,000 a year to reduce class size, while the city has pledged $500 million to make a bid for the 2016 Olympic Games.<p>
Compounding delegates' anger was Stewart's own role in raising expectations for the contract. She began talking about a possible strike months ago in the course of a two-year contract campaign, during which 100 contract demands were collected from rank-and-file teachers. But few of those demands made it into the proposed contract.<p>
The last time a CTU contract was rejected, it was proposed by the reform leadership of former CTU President Deborah Lynch in 2003. Lynch negotiated another concessionary contract that passed, but union members ousted her in the 2004 CTU elections and replaced her with Stewart. A product of the union's conservative old guard, Stewart was re-elected this year amid tough talk about the contract.<p>
Now, though, Stewart faces the possibility that rank-and-file union members will reject the deal in school-based votes set for September 10.<p>

