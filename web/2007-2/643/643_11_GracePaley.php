<font face="Arial, Helvetica, sans-serif" size="2"><b>OBITUARY</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Grace Paley: Passion for art and activism</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Kate Magill</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 7, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">GRACE PALEY, who passed away August 22, will be remembered for her distinctive literary voice and for her unflagging commitment to activism.<p>
Born in 1922 in the Bronx to a family of Russian Jews who had been opponents of the tsar, Paley (born Grace Goodside) grew up in a working-class immigrant neighborhood where vibrant political debate was commonplace. "I thought being Jewish meant you were a socialist," she once said. "Everyone on my block was a communist or a socialist...People would have serious, insane arguments, and it was nice."<p>
Paley's childhood shaped her literary voice as well as her political sensibilities. Her fresh, colloquial manner, combined with an almost intuitive literary economy, makes Paley's work eminently readable.<p>
Burlington activist Robin Lloyd put it this way: "Every time I hear [Paley] speak or read her poetry, she's able to cut through the jargon...She gets to the kernel of it and describes it in a way that reaches your heart, with simplicity, directness and this nitty-gritty realism that's so appealing. Her poetry is one with herself--and how she looks and how she dresses, there's no artifice."<p>
Perhaps some of that unpretentiousness comes from the fact that Paley pursued little formal literary education. A bright but disinterested student, Paley graduated from high school at 15 and briefly attended Hunter College, dropping out after one year. She married at age 19, and raised two children and worked as a typist. Paley and her first husband separated soon after the birth of their children.<p>
Paley began writing poems in her youth, but for years did so secretly. When her work finally appeared in print, in 1959, it was a collection of stories, <i>The Little Disturbances of Man. </i>Writing was a passion, but only one element of Paley's life, vying for time with family, work and activism. She explains, "Art is too long, and life is too short. There's a lot more to do in life than just writing."<p>
From the mid-1950s onward, Paley protested against militarization and in defense of women's rights. In 1954, she helped organize one of the first abortion rights speak-outs in the U.S. She opposed the Vietnam War, joining marches and traveling to Hanoi as a member of a 1969 peace delegation.<p>
After the mass struggles of the '60s, Paley remained active, protesting nuclear proliferation, supporting struggles against U.S. imperialism in Latin America, and carrying on the fight for women's liberation. In recent years, she was an outspoken opponent of the Iraq war.<p>
In <i>Just As I Thought</i>, a 1998 anthology of her lectures, articles and essays, Paley described herself as a "member of an American movement, a tide really, that rose out of the civil rights struggles of the fifties, rolling methods and energy into the antiwar, direct-action movements in the sixties, cresting, ebbing as tides do, returning bold again in the seventies and eighties in the second wave of the women's movement--and from quite early on splashed and salted by ecological education, connection, and at last action."<p>
She understood keenly the connections between art and social movements, asserting, "There has to be some political movement before there can be a literature. Black literature came because first there was a Black political movement. Women's literature came with a women's movement."<p>
Even while battling breast cancer, Paley remained politically active; earlier this summer she participated in an antiwar sit-in at the office of Rep. Peter Welch (D-Vt.). Paley made her home in Thetford, Vt., for the past 25 years, serving as the state's poet laureate from 2002 to 2007. With Grace Paley's passing, we have lost an artist and activist of great verve; it is up to all of us to keep alive her compassionate spirit and her dedication to radical grassroots change.<p>

