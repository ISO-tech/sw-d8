<p><a name="ICE"></a><font face="Times New Roman, Times, serif" size="4"><b>No more ICE raids</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Shane Johnson</b></font><p>

<font face="Times New Roman, Times, serif" size="3">FAIRFIELD, Ohio--On short notice, a dozen activists organized to oppose a raid by Immigration and Customs Enforcement (ICE) at a Koch Foods, a poultry packaging facility here.<p>
An army of more than 300 local and federal officers swarmed the plant around 10 on August 29. Some workers tried to hide in a sub-zero freezer in an effort to escape the raid and had to be treated for hypothermia. In all, ICE arrested approximately 160 workers from Koch Foods, making it one of the largest raids of the year.<p>
According to <i>Forbes </i>magazine, Koch, an anti-union company, had revenues of $1.4 billion in 2005. Yet days after the raid, it was revealed by state officials that the raid cost the state of Ohio a staggering $4 million.<p>
Butler County Sheriff Rick Jones announced later that, "If you come to this county, you can't come here and commit a crime and go unscathed." However, the real perpetrators of a crime are those willing to spend $4 million locking up workers for trying to provide for their families.<p>
The Coalici&oacute;n por los Derechos y la Didnidad de los Inmigrantes (Coalition for Immigrant Rights and Dignity) in Cincinnati was instrumental in organizing a rapid response press conference and protest. Activists are now trying to assist the families of those arrested by collecting food, household cleaning supplies, phone cards and money.<p>
<i>Send donations and messages of solidarity to: Butler and Warren County Workers Center, 18 S. 3rd St., Hamilton, OH 45011 or call 513-737-2327</i>.<p>
