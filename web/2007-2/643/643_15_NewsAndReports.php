<p><a name="BlueCross"></a><font face="Times New Roman, Times, serif" size="4"><b>Protesting Blue Cross/Blue Shield</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Glenn Allen</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--Approximately 50 people rallied outside Blue Cross/Blue Shield (BC/BS) headquarters August 29 to demand single-payer health care reform. This was the first public protest by the newly formed Chicago Single-Payer Action Network (CSPAN).<p>
The picket was loud and spirited. Several BC/BS executives came out and were subjected to chants of "Hey Blue Cross, what do you say, how many claims did you deny today?" and "People over profits, single payer now."<p>
Speakers at the rally included representatives from Physicians for National Health Program, the Progressive Democrats of America, the Green Party, Access Living (a disability-rights organization) and many others. Several speakers called for single-payer health care, cautioning against health care "reform," which includes private insurance.<p>
A statement of solidarity was read from Donna Smith, who is featured in Michael Moore's <i>Sicko</i>. State Representative Mary Flowers also attended the demonstration. Flowers is sponsoring House Bill 311, which would institute single-payer insurance in Illinois.<p>

