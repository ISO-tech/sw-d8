<a name="Newark"></a><font face="Times New Roman, Times, serif" size="4"><b>Newark, N.J., antiwar march</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Chris Dugan</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEWARK, N.J.--On August 25, the People's March here commemorated the 44th anniversary of the 1963 March on Washington for Jobs and Freedom; the 40th anniversary of the Newark rebellion; and the second anniversary of Hurricane Katrina.<p>
More than 1,000 people turned out to demand that funding for the war be redirected toward domestic needs. The march was the largest in Newark in decades and was organized by the People's Organization for Progress. Participants included members of Military Families Speak Out, Iraq Veterans Against the War, Veterans for Peace, the Nation Of Islam, the Troops Out Now Coalition, United For Peace and Justice, United Food and Commercial Workers and many others.<p>
The call for the impeachment of Bush and Cheney was the most visible demand of the march. This message was made strikingly clear when U.S. Rep. John Conyers--who had Cindy Sheehan and 46 others arrested in his office in July when they demanded that he begin impeachment hearings--opened his speech with the question "What do we want?" Apparently, he expected "Peace"--but he was greeted with a cry of "Impeachment" instead.<p>
Billie Spaight, who came to the march with the group Health Care-Now, spoke about how we "live in a messed-up society more concerned with killing than helping people."<p>
Margaret Stevens, national treasurer of Iraq Veterans Against the War, inspired numerous standing ovations while addressing the crowd in which she spoke about the links between the struggle to end the war, health care, immigrant rights and the international character of these struggles.<p>
As Margaret later explained, "The main point I wanted to communicate was that 'ending the war' in Iraq would not be addressing the root of the problem; namely, global competition for control of the world's oil resources...<p>
"So much of the antiwar movement is based around the moral sentiment that U.S. soldiers are being mistreated, which is--of course--accurate. And yet, there's this larger fact that U.S. soldiers are committing genocide in Iraq and we need to combat this racist, genocidal war because it hurts people the world over."<p>
