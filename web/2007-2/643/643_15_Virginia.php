<font face="Times New Roman, Times, serif" size="5"><b>Immigrants challenge racist law in Virginia</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Bel&eacute;n Cadena and Shane Dillingham</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 7, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">MANASSAS, Va.--In response to a vicious anti-immigrant resolution passed by the Prince William County Board of Supervisors, activists took to the streets over Labor Day weekend in protest.<p>
The measure, which passed July 10, denies public services to anyone who can't produce proof of citizenship and deputizes local police to work as immigration agents.<p>
On September 2, an estimated 15,000 people marched along the highways of this suburban community outside of Washington, D.C., in Northern Virginia, to demand that the resolution be rescinded. Speakers denounced the racism that was behind the resolution and the racism that it would promote through racial profiling, separation of families and fear of requesting basic services like going to a doctor.<p>
A large banner read, "Our bothers and sons are dying in war and we are being persecuted and deported." Signs in Spanish and English had slogans including "We're not criminals, we're not delinquents, we are workers and demand respect for our people," "No to the separation of families," and "The Constitution applies to all of us."<p>
One Native American welcomed the audience to his land. Larry Bell, an African American teacher in the area, connected the struggle of immigrants to the legacy of Dr. Martin Luther King and the struggle for civil rights. "Just as Blacks were harassed due to the color of their skin," said Bell, "this resolution harasses decent, hard working people as regards to their status."<p>
Other community members spoke about the essential role that immigrants play in the local economy.<p>
The march was spearheaded by Mexicanos Sin Fronteras (MSF), a local organization that has come under attack in the press for their work unapologetically defending immigrants and demanding amnesty.<p>
MSF held a series of mass meetings in neighborhood churches and community centers where thousands of immigrants attending voted to fight against the resolution by organizing this march and rally; holding a boycott of businesses that do not place an immigrant friendly sign in their window; and striking on October 9 if the resolution is not rescinded.<p>
With this resolution, the anti-immigrant right may have gone too far. While the right's intentions have been to instill fear amongst immigrants, the immigrant community has demonstrated that it refuses to lay low or be silent. The 15,000 people marching on Saturday show the potential for larger actions and the possibility of defeating the resolution.<p>

