<font face="Times New Roman, Times, serif" size="5"><b>How the banks want to make us pay</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 7, 2007 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">ONE OF the biggest questions about the meltdown in sub-prime mortgages is whether that crisis will be felt by the millions of workers who don't own homes. The answer, sadly, is yes. And the effects will become apparent very shortly--perhaps the next time you check your mailbox.<p>
Very quietly, financial institutions that have been exposed to losses in the mortgage market are looking to make that money up elsewhere--namely, by jacking up interest rates on credit cards. Capital One Bank, for example, recently shuttered its GreenPoint Mortgage subsidiary, which it acquired just three years ago as part of a $13.2 billion purchase, after the unit began hemorrhaging cash a few months ago.<p>
When it announced it was closing the unit, it also said it would begin jacking up the rates on its credit card unit, even for customers that had never missed a payment. For instance, according to TheStreet.com, a cardholder with access to credit at an annual interest rate of 4.99 percent will soon have to pay 13.9 percent.<p>
Capital One is the fifth-largest issuer of credit cards in the country. But it's not alone. American Express has increased its late fees. And Bank of America has also increased rates. Undoubtedly all other credit card issuers will follow suit.<p>
This couldn't come at a worse time for American workers. The savings rate is already negative for the first time since the Great Depression. Higher interest rates on credit cards will only increase the debt burden.<p>
Workers have been forced to turn to credit to pay for basic necessities. And faced with the prospects of ever increasing energy and health care costs and continued stagnating wages it will only add another burden to already stretched workers.<br>
<b>Petrino DiLeo</b>, New York City<p>

