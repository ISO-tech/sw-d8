<p><a name="Great"></a><font face="Times New Roman, Times, serif" size="4"><b>Great work on <i>SW</i>'s reviews</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WITHOUT EXCEPTION, the reviews published in <i>Socialist Worker</i> have been insightful and well written--even hilarious at times.<p>
One particularly fantastic review was published in the August 31 issue. Conor Reed's piece on M.I.A's newest album release, <i>Kala</i> ("A soundtrack for struggle"), was the best music review that I've read in a long time. Too often, music reviews in the "establishment" press artificially divorce the artist (and her work) from her everyday political and social reality, and therefore leave us with an incomplete portrait of the art that she produces.<p>
Reed combines his spot-on and compelling analysis of the actual music on the disc with an overall approach that explains the larger social, political and historical dynamics that inform M.I.A's style and sound. Keep up the great work!<br>
<b>James Fiorentino</b>, Amherst, Mass.<p>
