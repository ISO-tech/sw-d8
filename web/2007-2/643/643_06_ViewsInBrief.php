<p><a name="Monstrous"></a><font face="Times New Roman, Times, serif" size="4"><b>The U.S. government's monstrous experiments</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THE IOWA state government has agreed to pay $925,000 to people who were involved in what was called the "Monster Study"--in which, in 1939, the University of Iowa abused orphans just to try to make them stutter.<p>
Many people don't know, or have forgotten, that our government and private institutions have performed human experiments on Americans. Many different groups--children, prisoners, pregnant women, soldiers, mental patients, comatose patients, etc.--have suffered for science, profit or to win the "Cold War."<p>
The Navy did mustard gas experiments on American sailors in the 1940s.The CIA did LSD experiments in the 1950s. The Massachusetts Institute of Technology and the Quaker Oats Company were involved with feeding radioactive cereal to retarded children in the 1940s and '50s.<p>
In the 1960s, the Willowbrook State School in New York performed hepatitis experiments on retarded children. In the 1970s, there was the "Stanford prison experiments," where students were made to become either guards or prisoners.<p>
And, of course, many people have heard about the Tuskegee syphilis experiments on Black men. There are many other examples.<p>
The U.S. government still has files on human experiments performed by the Japanese on prisoners during the Second World War, which, for some reason, they haven't released. They should declassify this information, and investigate and release all information on past human experimentation by our government, and American schools, hospitals and corporations.<p>
We must never forget the people who lost their minds, health and sometimes their lives because of human experimentation.<br>
<b>Chuck Mann, </b>Greensboro, N.C.<p>

