<a name="Guards"></a><font face="Times New Roman, Times, serif" size="4"><b>The struggle facing security guards</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I'VE BEEN a worker at a private security company called Northwest Security Services Inc. in Seattle for about five years now. I enjoy the work that I provide at the Smith Tower very much on many different levels. I get to interact with strangers from all over the world everyday, and I get to assist them wherever they need to go.<p>
As security officers, we are responsible for the operations of the site. We literally control the building during our shift and monitor access control. This means that we are always here to provide help to any of our tenants located in the building. We conduct escorts, interior roves, exterior patrols and give assistance to contractors. We pretty much do it all. We are here for the tenants, but we are also here to guarantee that this $48 million building doesn't stop running for any reason.<p>
Since last summer, Service Employees International Union (SEIU) Local 6 has been actively trying to organize the local security companies, and so far, they have had some success. Workers from other security companies have really taken the initiative in demanding better wages, better medical benefits and better conditions at the work sites. They have organized themselves together, and are now in negotiation with their employers for the first master contract.<p>
We are now demanding the same for Northwest Security Services, but my security company has been on the offensive, denouncing everything that spells union for them. They have publicly stated in a recent newsletter given to the officers that "no officers at Northwest Security Services has shown any interest in ever joining the union."<p>
I would love to see the day when security officers in Seattle are no longer treated and disregarded as expendable tools of the clients, and are instead treated as professionals who are valued.<p>
Everyone working in downtown Seattle and Bellevue counts on security officers to keep their property and businesses safe and secure, and now the security officers need their community, comrades and friends to stand up for them in their fight to raise their standards of living and fight for more than subsistence wages, affordable health care, holidays, paid sick days and respect.<p>
This struggle for the workers is all new for me, but it's a struggle that I am willing to take on to the very end.<br>
<b>Ruben Bustamante</b>, Seattle<p>
