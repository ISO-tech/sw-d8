<font face="Times New Roman, Times, serif" size="5"><b>CIA torture cover-up?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 14, 2007 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>NICOLE COLSON reports on the scandal over destroyed videotapes of interrogations.</b><p>

THE SCANDAL over the U.S. torture of detainees in the "war on terror" took a new twist December 6 when news broke that the CIA purposely destroyed hundreds of hours of videotapes showing interrogations of alleged al-Qaeda operatives.<p>
The videotapes apparently are of two prisoners, Abu Zubaydah and Abd al-Rahim al-Nashiri, who were among the first to be detained and interrogated by the CIA in secret prisons after the September 11 attacks.<p>
According to the <i>New York Times</i>, while it's not known what was on the tapes, they reportedly showed "severe interrogation techniques" being used on both men. It is known that CIA interrogators used "stress and duress" techniques--including noise at high volumes, stress positions, isolation and waterboarding--on Abu Zubaydah, in particular.<p>
Besides the question of human rights abuses, what was on the tapes is important in another respect. At least five lower-level detainees at Guant&aacute;namo Bay, Cuba, were charged with offenses based on information provided by or related to Abu Zubaydah. Lawyers for Guant&aacute;namo detainees say that seeing how he was treated could provide evidence as to whether he may have given false information to stop from being abused by his interrogators.<p>
CIA officials were warned in 2003 by members of Congress--including Rep. Porter Goss, who later went on to become the director of the CIA--as well as by Justice Department and White House lawyers, not to destroy the tapes.<p>
But in November 2005, Jose Rodriguez Jr., then the chief of the CIA's clandestine service, decided to have the tapes destroyed--even as questions of torture and abusive interrogation practices were being investigated by Congress.<p>
In a note to his staff after the news broke that the tapes had been destroyed, current CIA director Michael Hayden stated that they were destroyed only in order to protect the identities of the CIA agents shown in the tapes. "Were they ever to leak, they would permit identification of your CIA colleagues who had served in the program, exposing them and their families to retaliation from al-Qaeda and its sympathizers," Hayden said in a memo.<p>
Sen. Carl Levin (D-Mich.) called this "a pathetic excuse. You would have to burn every document in the CIA that has the identity of an agent on it, under that theory."<p>
As the <i>Washington Post</i> wrote, "It is far more plausible that CIA officials eliminated evidence that could have been used to hold interrogators accountable for illegal acts of torture--as well as the more senior administration officials who ordered or approved those acts."<p>
For its part, the Bush administration has said only that the president had "no recollection" of being made aware of the tapes' destruction before December 6, when Hayden briefed him on the matter. The question now is if someone from the White House or the agency will come forward to contradict that story.<p>
Congressional Democrats have asked for a Justice Department investigation to determine if CIA officials committed obstruction of justice by destroying the tapes. A Senate Intelligence Committee and internal CIA investigation are also both reportedly underway.<p>
The drive to cover up evidence of torture and harsh interrogations is a direct result of the Bush administration's insistence that the Geneva Conventions not apply to prisoners of the "war on terror"--and to classify waterboarding and other harsh interrogation methods as something other than torture.<p>
And the Democrats, who are now expressing their "outrage," have done little to challenge the administration's human rights abuses. In fact, just days after the revelations about the videotapes' destruction, it was revealed that current House Speaker Nancy Pelosi (D-Calif.) not only knew about the administration's use of waterboarding as early as 2002, but supported it.<p>
According to the <i>Washington Post</i>, Pelosi, then a senior member of the House Intelligence Committee, was informed by CIA officials at a secret briefing in September 2002 that waterboarding and other forms of "enhanced interrogation techniques" were being used on suspected al-Qaeda operatives.<p>
In a closed-door session, Pelosi, along with then-Rep. Porter Goss and Sens. Pat Roberts (R-Fla.) and Bob Graham (D-Fla.), were told what the CIA was up to. According to the <i>Post</i>, "Individual lawmakers' recollections of the early briefings varied dramatically, but officials present during the meetings described the reaction as mostly quiet acquiescence, if not outright support."<p>
As Goss said, "Among those being briefed, there was a pretty full understanding of what the CIA was doing. And the reaction in the room was not just approval, but encouragement."<p>

