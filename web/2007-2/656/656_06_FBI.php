<font face="Times New Roman, Times, serif" size="5"><b>Harassed by the FBI for standing up to racism</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 14, 2007 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">I WAS visited by the FBI at my residence on Thursday, November 29. I am an Asian-American Muslim man, and an antiwar activist who believes that the U.S. military has no business in the Middle East or anywhere else in the world. I do not hide my political perspectives from anyone. I refuse to be intimidated into silence.<p>
The two FBI agents claimed to be investigating a "complaint" from University of Washington (UW) students that I might have said "things" which could suggest that I advocate violence against the U.S. This was in reference to my opposition to the "Islamofascism Awareness Week," which the College Republicans hosted in mid-October.<p>
During this week, the College Republicans showed a video that described all Palestinians as terrorists and posited a theory that Palestinian men are prone to violence because they are sexually repressed (ironic, coming from the College Republicans). Michael Medved also spoke during this week.<p>
I was part of a demonstration with a group of Muslims, students and people of color who were barred from entering the event. The event organizers cited full capacity as the main reason. Why is my opposition to this event being criminalized?<p>
The FBI agents wanted to see a flyer that I was passing out. I told them no. I asked them, "Is this Stalin's Russia or McCarthy's America?" They have no business collecting literature from me or anyone else in the Untied States. Anyway, the flyers can be found all over campus, because I have nothing to hide from my neighbors.<p>
This is not one isolated incident. Muslims and Arabs are being attacked, harassed and intimidated by the FBI across the country. The FBI claims that anyone who opposes U.S. imperialism is a terrorist. Some Muslim leaders have come out in public and said "good Muslims" should cooperate with the FBI to help them control the "bad Muslims."<p>
Meanwhile, the FBI has sent spies to our mosques and broken into our houses in the middle of the night to kidnap our brothers and sisters and send them to Guant&aacute;namo without evidence of any crime. Who is causing the terror here? Most of the time, it goes unnoticed, and in the shadows, Arab and Muslim families are destroyed.<p>
Let me make it clear--the FBI is a racist and anti-democratic organization. Granted, the two FBI agents who visited my house were very nice to me and even shook my hand. That should not cover up the crimes of the institution they work for.<p>
Meanwhile, the College Republicans, who claim to be defenders of democracy and free speech, are inviting racists like Medved onto campus. The College Republicans advocate perspectives that lead to the deaths of Arabs and Muslims and people of color, and you don't see the FBI visiting them. I am not advocating state repression against the College Republicans; I simply wish to point out the racist double standard here.<p>
How did the FBI get my house address? My address is not listed anywhere. Did the university administration give it to them? This would not be farfetched considering that university administrations across the country have made it clear that they will stand with the FBI before they stand in solidarity with Arabs, Muslims and students of color. Just look at their endorsement of the USA PATRIOT Act and the SEVIS registration system for international students.<p>
Given this climate, the burden should be on the UW administration to prove that they are not collaborating with such McCarthyist surveillance of campus activists like myself. How can they claim to be the patrons of free speech and dialogue if they facilitate such intimidation?<p>
Only democratic and anti-racist students can curb the power of racist university bureaucracies and the FBI. Student organizing is part of a rich tradition of American history that has made the U.S. a more democratic and anti-racist country than it would be otherwise.<p>
However, across the country, university administrators and the FBI are working hand-in-hand to shut down and intimidate all who oppose U.S. empire and domestic racism.<p>
UW and the city of Seattle claim to be liberal and progressive places where racism cannot be found. This is a myth. Homegrown white supremacy stalks this campus. Every time we see the College Republicans, the FBI and the UW administration, it is a reminder that people of color and Muslims and Arabs are not safe yet.<p>
However, we are not silent victims. We do not know our own strength, and no one dares to tell us. It is up to us to rediscover our democratic and anti-racist traditions. It is up to us to take back our university. Fear is not an option.<br>
<b>Shemon Salam</b>, Seattle<p>

