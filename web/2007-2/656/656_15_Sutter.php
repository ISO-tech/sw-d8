<font face="Times New Roman, Times, serif" size="5"><b>Nurses set for second walkout at Sutter</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Phillip Molnar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 14, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">REGISTERED NURSES are set to strike December 13 and 14 against the biggest health care hospital network in Northern California, Sutter Health. A walkout is aimed at involving 5,000 RNs at 13 hospitals across the Bay Area.<p>
"They're going after our health care, our pension, our livelihood," said Bob Auen, a nurse at Eden Hospital in Castro Valley. "We are 5,000 nurses, a strong coalition, and we will stay united."<p>
The strike, announced by the California Nurses Association/National Nurses Organizing Committee (CNA/NNOC) on November 30, will involve some of the biggest hospitals in the Bay area: California Pacific Medical Center, Alta Bates Summit Medical Center, and Mills-Peninsula Health System.<p>
The proposed action follows the October 10-11 strike, which led to contract talks at most of the hospitals, but left most of the nurses' demands unmet: safe staffing at all times, improved retirement security, and secure health benefits for the RNs.<p>
The RNs' action in October was one of the largest strikes in the U.S. in the past decade. It included 5,000 nurses and won 95 percent of nurses to the picket line. Still, Sutter Health has not met demands. Two of Sutter's hospitals, Sutter Delta and Sutter Solano, have refused to hold any additional negotiations at all.<p>
"Sutter seems unable to recognize how seriously the nurses take our commitment to our patients and our communities," said Zenei Cortez of CNA/NNOC's Council of Presidents. "The Sutter RNs and our entire organization stand united behind our campaign for safe patient care, security for the RNs, and continuation of needed medical services for the patients and communities Sutter is threatening to abandon."<p>
The Sutter Health network earned $587 million in profits last year, but that hasn't stopped them from routinely understaffing, closing community hospitals located in underserved communities, and attempting to cut the healthcare of the caregivers.<p>
Sutter plans on closing three more hospitals in San Francisco, San Leandro and Santa Rosa, apparently because they aren't making enough money.<p>
"As patient activists, now is our time to stand up and force Sutter Health to ensure that every patient, in every hospital, receives the quality of care they deserve. It's unfortunate that this has to come about through a strike, but it is the only way to force Sutter to do the right thing," said Summit RN Efren Garza.<p>
The strike is also set to involve RNs at Sutter Marin General Hospital in Greenbrae, and Sutter Novato, and members of the CNA/NNOC-affiliated Caregivers and Healthcare Employees Union, representing respiratory, X-ray, and other technical workers at Alta Bates Summit.<p>
"Sutter Nurses are united across the region because these issues are too important to let go," said Auen.<p>

