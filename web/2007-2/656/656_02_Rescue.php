<font face="Times New Roman, Times, serif" size="5"><b>A "rescue" that leaves victims behind</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 14, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">GEORGE W. BUSH'S message to sub-prime mortgage holders about to lose their homes? Drop dead.<p>
The "emergency rescue" deal offered by the biggest lenders and brokered by Bush and Secretary Treasurer Henry Paulson "won't provide relief to many subprime-mortgage holders," the <i>Wall Street Journal</i> noted.<p>
"These include borrowers who are now in foreclosure, have already refinanced their homes, or are more than 60 days delinquent on more than one payment over the past year. In some cases, people with good credit scores will be excluded. Also left out are those deemed able to afford the higher interest rates scheduled to replace their introductory rates over the next two years."<p>
Covered under the proposal are borrowers who took out sub-prime adjustable rate mortgages (ARMs) between January 1, 2005 and July 31, 2007, with interest rates to be reset between January 1, 2008 and July 31, 2010. There's a further catch: loans are covered only if they've been repackaged into securities and sold off to investors.<p>
Some 1.2 million people are eligible to apply for relief--even though 1.8 million sub-prime borrowers have adjustable rate mortgages (ARMs) in which interest rates will rise over the next two years, and there are plenty of prime borrowers facing troubles, as well.<p>
And of the 1.2 million, just 600,000 are expected to qualify for automatic relief under standards set by the Financial Services Roundtable, a group comprised of the country's 100 biggest banks and other companies involved in mortgage lending.<p>
Those with low credit scores who have less than 3 percent equity in their homes will get five-year extension of mortgage payments at the introductory interest rates, after which the higher rates will still kick in. Those who don't qualify for that program will get something they already had--the "right" to negotiate individually with loan-servicing companies to keep their current interest rate. But if the servicing companies determine that the mortgage-holders can afford the higher rates, they'll still have to pay more whenever the ARM resets.<p>
Essentially, the rescue gives the industry the means to cherry-pick borrowers that they deem can still afford to pay their mortgages--and write off the rest as lost causes.<p>
Those who won't suffer are the financial executives who profited from the push to market high-interest subprime loans, even to borrowers who could have qualified for lower interest rates.<p>
"At the center of the boom in mortgages for borrowers with weak credit was Wall Street's once-lucrative partnership with sub-prime lenders," the <i>Wall Street Journal</i> noted. "This relationship was a driving force behind the soaring home prices and the spread of exotic loans that are now defaulting in growing numbers. By buying and packaging mortgages, Wall Street enabled the lenders to extend credit even as the dangers grew in the housing market."<p>
That strategy paid off--for the bankers. According to one study, the average annual compensation for managing directors for mortgage divisions of the big investment banks was $2.52 million in 2006. This year, however, times are hard--so their pay is expected to be only a little more than $1 million.<p>

