<font face="Times New Roman, Times, serif" size="5"><b>Writers dig in for a long battle</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 14, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>CINDY KAFFEN reports on the ongoing writers' strike after produers walked out of negotiations.</b><p>

LOSING THE battle of public opinion and faced with a near complete shutdown of television production and an unexpected display of solidarity by writers, negotiators for television and film production companies walked out of talks with the Writers Guild of America (WGA) December 8.<p>
The media moguls went on the offensive after eight days of meetings in which their organization, the Alliance of Motion Picture and Television Producers (AMPTP), rejected the WGA's proposals on Internet streaming, refusing to increase the rate for downloads or to cover original material converted to new media.<p>
Instead, AMPTP walked out with an ultimatum to the union--drop six of your demands, or we won't return to discuss anything.<p>
Among the points the employers found so offensive were WGA demands that would strengthen the union movement in Hollywood. One was to include reality television writers and animation writers (two areas where television production is booming) into the WGA's jurisdiction.<p>
Another was the union's insistence on not having a no-strike clause in the new contract--which would allow the writers to act in solidarity when other unions find themselves having to strike for fair deals, as is widely expected of the Screen Actors Guild when its contract runs out at the end of June.<p>
In a press statement conveniently released moments after walking out of the negotiating room--leading many to suspect that the walkout had been planned in advance--the AMPTP declared, "The WGA's organizers are determined to advance their own political ideologies and personal agendas at the expense of working writers and every other working person who depends on our industry for their livelihoods."<p>
As if Les Moonves, the CEO of CBS, who made over $28 million last year and is now laying off television production staff and crew members right before Christmas, is the one that really cares about working people!<p>
Having announced the hiring of the high-priced crisis management firm Fabiani and Lehane (known in Washington, D.C., political circles as "the masters of disaster"), the industry bosses clearly hope to break the solidarity of the writers and their supporters among the other entertainment industry unions by attacking the competence and motivation of the Guild's leadership.<p>
Fabiani and Lehane are closely connected with the Democratic Party and worked for Bill Clinton's administration. Other past clients include Al Gore, Hillary Clinton, Michael Moore for <i>Fahrenheit 9/11</i> and <i>Sicko, </i>and even the Screen Actors Guild in 2002. Apparently union-busting is now part of the firm's expertise as well.<p>
"It's classic strike-breaking tactics," striking writer Pat Mulvihill said. "They try to build up our hopes, and then walk out. They're trying to force cracks in the union, and we fully expect them to do it again. Just like with the AMPTP statements that the union leadership is being unreasonable, and that the 'below-the-line' workers are getting shafted by us striking. It's all about divide and conquer. But it's not working."<p>

