<font face="Times New Roman, Times, serif" size="5"><b>HIV/AIDS epidemic as bad in D.C. as Africa</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Chris Yarrison</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 14, 2007 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">HIV INFECTION rates for Washington, D.C., residents rank between those for the Republic of Congo and Rwanda. That's the horrifying calculation of a new study released by the District's HIV/AIDS Administration.<p>
One in every 20 D.C. residents is infected with HIV, according to the report, and one in 50 have AIDS, the advanced manifestation of HIV. The infection rate is higher than any other U.S. city, and 10 times the national average. "HIV/AIDS has become a modern epidemic with complexities and challenges that continue to threaten the lives and well-being of far too many residents," the report concludes.<p>
The disease disproportionately afflicts Black residents, who are 80 percent of HIV/AIDS cases, but less than 60 percent of the city's population. Among infected women, 90 percent are Black.<p>
According to the study, 37 percent of cases were transmitted through heterosexual contact, compared to 25 percent spread between gay men--statistics that "blow the stereotype out of the water," in the words of Shannon Hader, the HIV/AIDS Administration's new head. "HIV is everybody's disease here."<p>
Nevertheless, the District's nonvoting Rep. Eleanor Holmes Norton offended the LGBT community by advertising a forum with a flier stating "STDs and HIV are now a threat to normal relationships."<p>
As the group Fight HIV in DC stated on its Web site, "The wording on the flier left little doubt as to what relationships Norton considered 'normal' and which she considered 'abnormal.' And her decision to ignore the HIV/AIDS epidemic among gay men and transgender individuals...is unacceptable."<p>
The group joined with DC Fights Back to stage a rally in front of the White House November 30 for World AIDS Day. The protest drew nearly 1,000 demonstrators from the city and the region and culminated in more than 40 participants engaging in civil disobedience. Earlier in November, the two groups held a local demonstration with more than 100 residents from diverse communities.<p>
Another major factor in AIDS transmission is the sharing of syringes, which accounts for 13.2 percent of cases, according to the report. The federal government doesn't allow funding for the District to have needle exchange programs, but one group, Prevention Works, has been able to operate a nonprofit program around the city. Traveling each day to high-risk areas in a modified RV, the group's approach has not only deterred needle sharing, but also introduces drug users to effective methods for getting clean.<p>
Although Rep. Norton has been visibly advocating AIDS education as well as needle exchange funding, she shows "a definite lack of communication and willingness to partner with HIV/AIDS organizations in D.C.," says Fight HIV in DC.<p>
Grassroots organizations like these know they can't count on local or federal officials to support an inclusive and effective fight against this tragic epidemic, although that fight may be long and painful.<p>

