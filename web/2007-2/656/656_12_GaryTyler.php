<font face="Times New Roman, Times, serif" size="5"><b>An appeal for justice for Gary Tyler</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 14, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">AMNESTY INTERNATIONAL is encouraging anyone who supports justice to call on outgoing Louisiana Gov. Kathleen Blanco to pardon Gary Tyler before she leaves office in January.<p>
In 1975, Tyler, then just 17 years old, was wrongly convicted of the murder of Timothy Weber, a 13-year-old white youth who was killed in 1974 during an attack by a racist white mob on a school bus filled with African American high school students in Destrehan, La.<p>
Tyler's trial was all too typical of Jim Crow "justice" in the South: He was tried in front of an all-white jury from which Blacks were deliberately excluded; several witnesses later recanted their testimony, claiming police coerced them; and Tyler's court-appointed attorney failed to interview any witnesses or prepare for the trial.<p>

