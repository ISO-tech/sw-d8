<P><font face="Times New Roman, Times, serif" size="4">Prosecutors won't retry former death row inmate</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Ryan's nightmare is over</b></font></P>
<font face="Arial, Helvetica, sans-serif" size="2">September 10, 2004 | Page 4</font>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
On August 9, the nightmare for former Louisiana death row prisoner Ryan Matthews finally came to an end. Seven years after he was arrested for a crime he didn't commit--just one month after his 17th birthday--Ryan became the 115th person exonerated and freed from death row in the U.S. since the reestablishment of the death penalty a quarter century ago.</P>
<P>Louisiana authorities reluctantly dropped their fight to retry Ryan after DNA evidence from the crime scene once again proved Ryan's innocence--after having been tested <I>six</I> times by the courts! District attorneys have been on an outrageous crusade to convict and execute Ryan ever since he was put behind bars in 1997 for the murder of a storeowner in a small town outside New Orleans. </P>
<P>Railroaded in a three-day trial by an almost all-white jury, Ryan--who is Black--became an easy scapegoat for the racist authorities in Jefferson Parish (Klan leader David Duke's hometown). But Ryan and his family have fought back since the day he was convicted in 1999. </P>
<P>Ryan refused to plead out his case, insisting on his innocence despite pressures even from his court-appointed attorney to admit guilt. And despite police intimidation, phone taps and false accusations, his sister Monique and mother Pauline have bravely spoken out all over the country about his case and against the death penalty.</P>
<P>Ryan's freedom is a huge victory for death penalty foes and has made lifelong fighters out of Ryan, his family and others who won't stop until we've abolished the death penalty for good. Death row exonerations are climbing every year. Ryan's case proves once again that our struggle has been right all along--the system's too broken to be fixed.<BR>
<B>Lee Wengraf, </B>New York City</P>


