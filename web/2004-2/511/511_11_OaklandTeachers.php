<P><font face="Times New Roman, Times, serif" size="4">School district pushes union-busting demands</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Oakland teachers fight back</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jessie Muldoon, OEA</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 10, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">OAKLAND, Calif.--About 400 people rallied and marched in support of public education here on Labor Day. The event was initiated by the Oakland Education Association (OEA) to bring labor unions, families, and community groups together to demand full funding for public education. Unions representing the full range of the employees in the school district, as did representatives of the International Longshore and Warehouse Union, the Alameda Central Labor Council and the Green Party.</P>
<P>Morris Tatum, the president of the American Federation of State, County and Municipal Employees, Local 257--representing food service, custodial service and aides to the handicapped--declared his union's solidarity in the event of a teachers' strike. "I want you to know that this is labor's united front and we need to get together like we never have before," he said. "If you decide not to teach, we will decide not to prepare food, we will decide not to clean. Don't let them divide and conquer. Remember that an injury to you is an injury to all. We know it's coming to us next."</P>
<P>The rally came just weeks after the OEA ceased negotiations with the Oakland Unified School District and declared an impasse. The union has been bargaining since March for a new contract for the 2004-05 school year.</P>
<P>The previous agreement expired June 30, and teachers and other staff are working under a contract extension. The district--run by state administrator Randy Ward--presented its initial proposal in the spring. It was a strictly concessionary package, with important protections like seniority wiped out. </P>
<P>The proposal would impose an additional $65-$200 in monthly health care costs on teachers and maintains the 4 percent pay cut that OEA members took last year. Other items on the chopping block include prep time for elementary teachers and the enrichment program (arts, music, physical education, etc) for elementary school students.</P>
<P>Teacher input or site-based union representation are eroded or eliminated in the district's proposal. The district also wants a one-year contract, creating a perpetual cycle of negotiations.</P>
<P>Incredibly, the district returned to the table mid-August with a new package of even deeper cuts. These include the elimination of personal leave and the creation of a process by which certain schools, formed on the "small schools" model, are waived from the majority of the contract. Teachers at such schools would have to sign an agreement that they accept the waiver or face reassignment.</P>
<P>In an effort to blame the union for the impasse, the district falsely claimed that the union walked out of talks after just one bargaining session. Many in the union believe that the district's proposal was a provocation. For this reason, the OEA is consciously trying to build links with the other unions represented in the school district, as well as with the Oakland community.</P>
<P>It's clear to the activists and leaders in the union that this will only be resolved with a real fight. Stella Collins, chair of the OEA bargaining team, expressed this point at the Labor Day Rally when she addressed the crowd: "You have given me the strength to say 'no' to the district's proposal."</P>
<I><P>Noah Brick contributed to this report.</I> </P>


