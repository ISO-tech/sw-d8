<P><font face="Times New Roman, Times, serif" size="4">David Potorti of September 11 Families for Peaceful Tomorrows:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"They'll use anything to consolidate their power"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 10, 2004 | Page 6</font></P>

<P><font face="Times New Roman, Times, serif" size="3">DAVID POTORTI, whose brother James died in the attack on the World Trade Center in 2001, is a cofounder of September 11 Families for Peaceful Tomorrows. David and others whose family members died that day have been outspoken opponents of the wars that the Bust administration has launched in their names. They recently took part in pulling a memorial stone from Boston to New York City--sites of the Democratic and Republican conventions--to commemorate the victims of violence and war around the globe.</P>
<P>Potorti spoke to <I>Socialist Worker's </I>ELIZABETH SCHULTE a week before the anniversary of September 11.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><P><B>WHAT DO you think about the way that the Bush administration has used the tragedy of September 11 to further its foreign policy aims?</B></P>

<P>FROM DAY one, we realized that the 9/11 attacks and the deaths of our family members were going to be used as soon as we went into Afghanistan. It became clear that there was a utility to these deaths that was going to be taken advantage of.</P>
<P>It's easy to blame the administration for that, but I think it's much bigger than just any administration. I think of it as the "military-industrial-media complex." I think it's just a natural state of affairs that the military and people with power will take advantage of anything to consolidate their power and turn people against each other.</P>
<P>It goes way beyond the current administration, which is just a representation of that power. The president always represents the people with power, and unfortunately, you and I don't have any at the moment. What is being represented is corporate power, military power and media power.</P>

<P><B>HOW DID your group come about?</B></P>

<P>IT BECAME clear that our people were going to be used as an excuse for those powerful forces to consolidate even more power. And that made me obviously very sad, because there was the personal connection with my brother who died at the World Trade Center.</P>
<P>Then there was a sense that his death and the deaths of all those other people were going to used to cause the deaths of other people that were just like them on the other side of the world. We launched September 11 Families for Peaceful Tomorrows on February 14, 2002, and our whole body of work has been about connecting with other human beings who have been affected by terrorism and violence and war, and finding a strength in that.</P>
<P>That includes people who were in Japan during the bombings of Hiroshima and Nagasaki, people in Israel and Palestine who formed a peace group called the Parents Circle. I went to South Korea last year, and I went to Japan. In Northern Ireland, I met a woman who reconciled with a woman who murdered her father.</P>
<P>We went to South Africa and talked to people who had made it through the anti-apartheid struggles and were now trying to reconcile with the history of their country and the murders that had been committed. We've gotten a lot of strength by hooking up with people just like us and recognizing that they're not the enemy.</P>
<P>There are some people who want to do us harm, but the vast majority of people are just like us--they want to live in a safe world, they want their children to grow up and have a good education, all the things that we all want. Personally, since I've made all those connections, I've become less angry, less afraid and less willing to just accept the lies that the world is against us, the world is a dangerous place, and we have to spend more money on the military machine.</P>
<P>I think that's the reason it's so difficult to tell our stories in the U.S., because people don't want to hear that. They accuse you of being "partisan" because you're criticizing the administration, and so your stories just don't get heard.</P>

<P><B>WHAT DO you think of the results of the commission to investigate September 11?</B></P>

<P>THERE WERE a couple things I really liked. There's a paragraph on page 364 which was an almost verbatim statement agreeing with what I said in the fall of 2001: "Long-term success demands the use of all elements of national power--diplomacy, intelligence, covert action, law enforcement, economic policy, foreign aid, public diplomacy and homeland defense. If we favor one tool while neglecting others, we leave ourselves vulnerable and weaken our national effort."</P>
<P>I think that is exactly what I was saying in 2001--that we can't just bomb people into peace. The other thing I noticed as I looked at the report is that it acknowledged that the Israeli-Palestinian conflict was at the center of anti-American sentiment in the Middle East.</P>
<P>They sort of mentioned it, but they didn't acknowledge that our policies are wrong--that we have to be realistic that these policies are causing anti-American sentiment. It's good to acknowledge that, but not to say that we need to rethink our policies toward Israel and Palestine was very disappointing to me.</P>
<P>Which leads me to a general comment about the report. To the extent that it doesn't deal with the why of terrorism--why people want to kill us, why terrorism is happening--to the extent that it doesn't deal with that, then I think it has failed.</P>
<P>Journalist Robert Fisk says that is the taboo--the one thing you can't talk about is the sources of international terrorism against the United States. That has to do with our foreign policy and the way that we relate with the world.</P>

<P><i>For information on September 11 Families for Peaceful Tomorrows, visit www.peacefultomorrows.org on the Web.</i></P>

