<p><font face="Arial, Helvetica, sans-serif" size="2">September 10, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Stop Chicago police torture</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Noreen McNulty</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--Dozens of protesters chanted "Police torture is a crime, why isn't Burge doing time?" outside the downtown Chicago law offices of Freeborn and Peters as notorious police torturer Jon Burge was questioned by attorneys September 1. Former death row prisoner Madison Hobley, along with family members of other torture victims, attended the protest called by the Enough Is Enough campaign and the Campaign to End the Death Penalty.</P>
<P>Madison and former death row prisoner Aaron Patterson have filed civil suits against Burge, detailing their torture by Burge in the police's attempt to coerce false confessions from them. These "confessions" were used to send both to Illinois' death row.</P>
<P>Using electro-shock, suffocation, severe beatings and death threats to obtain confessions, Burge and police under his command tortured more than 100 suspects at the police's Area 2 and 3 headquarters on the South Side of Chicago. Several of these men landed on death row for crimes they did not commit. </P>
<P>At his deposition, Burge was questioned for four hours and pled the Fifth Amendment to nearly all questions. "Why is he taking the Fifth?" asked Joan Parkin, of the Enough is Enough Campaign. "What is he afraid of? [That] he'll be asked questions like, 'did you torture anybody?'"</P>
<P>Madison Hobley recalled the horror of torture at the hands of Burge and his accomplices. "I remember how they took me into the utility room and beat me and suffocated me," he said. "And then to top it off, hid evidence and lied about me. It sent me to prison for 16 years."</P>
<P>As Burge was leaving the law offices, he was issued a subpoena to testify in front of a grand jury as part of an investigation of police torture. Two years ago, lawyers and activists won a special prosecutor to investigate the allegations.</P>
<P>Activists from the Campaign to End the Death Penalty and the Enough Is Enough Campaign vow to keep up the pressure. On September 18, the Campaign to End the Death Penalty will hold a panel on forced confessions, featuring family members of torture victims, activists and lawyers.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Vote Nader 2004</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Kolya Ludwig</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEATTLE--The hard work of a few individuals brought the Nader-Camejo ticket to the Washington State ballot. With 1,983 signatures, the Nader campaign almost doubled the amount required for a presidential candidate to get on the ballot here. However, the chairman of the state's Democratic Party, Paul Berendt, is leading the legal charge to erase our summer's work--and Nader's progressive candidacy from the ballot.</P>
<P>Meanwhile, Nader campaign meetings have been moved to Seattle Central Community College, a commuter campus in the heart of the city. This should bring newer faces to our struggle and give confidence to the movement behind the pro-worker, antiwar ticket of Ralph Nader and Peter Camejo.</P>


