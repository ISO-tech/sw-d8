<P><font face="Times New Roman, Times, serif" size="4">How many innocent lives were destroyed?</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Houston cops' "legacy of fraud"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By John Green</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 10, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">HOUSTON POLICE have admitted that they discovered mislabeled and improperly stored evidence from 8,000 criminal cases that may provide answers in hundreds of pending innocence claims. The evidence from the 8,000 cases is stored in 280 boxes and spans the years 1979 to 1991. </P>
<P>Boxes are supposed to hold evidence from only one case. The discoveries include a human fetus, body parts and potential clues that could reopen murder investigations. The boxes were found <I>last year</I>--but police claim that they didn't realize it because the boxes were mislabeled.</P>
<P>This admission deepens a crisis that has been ongoing since the department crime lab's DNA section was shut down following a December 2002 audit showing that personnel were poorly trained, failed to label evidence properly and may have exposed evidence to contamination. Barry Scheck, director of the Innocence Project, cited the revelations as further proof of the Houston Police Department's "legacy of fraud, incompetence and confusion."</P>
<P>In March, then-Police Chief C.O. Bradford asked lawmakers to postpone the executions of seven death row inmates pending a review of DNA tests. Days later, Josiah Sutton was released when DNA re-testing cleared him. Convicted of rape at age 16, he had served four-and-a-half years of a 25-year sentence. </P>
<P>Prisoners with innocence claims have requested new tests in a total of 379 cases. But the police department says that evidence has been destroyed or lost in many of these contested cases. Some of this evidence may be in the "newly found" boxes, but many prisoners will have no recourse to question their convictions.</P>
<P>As early as September 1999, six lab workers complained to then-Chief Bradford that the lab was "a total disaster." One of the workers was later reprimanded in June 2003 for "incorrectly documenting DNA results" in two sexual assault cases. Two months ago, a state judge accused former DNA lab chief James Bolding of aggravated perjury during a sexual assault trial. Bolding escaped punishment because the statue of limitations had expired.</P>
<P>After dragging their feet in announcing the discovery of an enormous cache of lost evidence--and admitting that they had locked up an innocent teenager in a rape case--the Houston Police Department stands exposed. Josiah Sutton lost nearly five years of his life. How many more lives were destroyed by the Houston cops?</P>


