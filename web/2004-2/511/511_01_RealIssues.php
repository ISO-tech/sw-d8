<P><font face="Times New Roman, Times, serif" size="4">Bush rants...Kerry blusters...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>They won't talk about the real issues</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 10, 2004 | Page 1</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THEY WANTED to play up the image of "compassionate conservatives." But no mask was big enough to hide the twisted face of the Republican Party at its convention last week.</P>
<P>There was former New York City Mayor Rudolph Giuliani, who shamelessly exploited the memory of September 11 to portray George W. Bush as a hero. There was the Governator, Arnold Schwarzenegger, who had a message for working people frightened about the state of the economy: Don't be "economic girlie-men." George Bush and Dick Cheney smirked their way through speeches that were supposed to outline their political "vision"--though they obviously got more pleasure out of bashing John Kerry.</P>
<P>But for off-the-charts lunacy, the main event was the keynote address by Zell Miller, the nutty Georgia senator who says he's leaving the Democratic Party because it's gone too far <I>to the left.</I> After his speech, the agitated senator told interviewer Chris Matthews, "I wish we lived in the day where you could challenge a person to a duel." </P>
<P>Now that the two main candidates have been anointed, they'll hit the campaign trail. But not everywhere--only in the select few "battleground" states where their advisers figure they have a chance of tipping the balance in the undemocratic Electoral College.</P>
<P>Their scripted, television-ready, focused-grouped appearances will be filled with talk about "leadership," "values," "character." Anything except the real issues that effect ordinary people's lives.</P>
<P>As their convention showed, the Republicans are chomping at the bit to prove that Kerry doesn't have what it takes to be commander in chief--that he's "soft on terrorism" and liberal to his core. Kerry set himself up for this attack. The Democratic convention a month earlier was devoted to trumpeting Kerry's record in the Vietnam War--as proof that he would be "tough on terrorism" and promote "conservative values."</P>
<P>Kerry is busy running away from any liberal vote he might have made in his 19 years in the Senate. And he's especially concerned about distancing himself from the the most decent thing he's done in his life--joining fellow soldiers to speak out against the Vietnam War after he was discharged.</P>
<P>Kerry is intent on showing how close he is to Bush, not how far away. If the candidates seem, despite their rhetorical attacks on each other, to be so similar, it's because neither party has a solution to the real problems all around them. The suffering of Iraqis enduring Washington's occupation. The misery of workers who can't find work that pays a living wage.</P>
<P>All Kerry can say is that he would have supported Bush's war even if he knew Iraq didn't have "weapons of mass destruction." To unemployed workers, Kerry says "more corporate tax breaks." We have to say "no" to the Republican and Democratic platforms--and build an alternative that fights over the issues that matter.</P>


