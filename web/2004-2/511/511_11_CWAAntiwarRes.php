<P><font face="Times New Roman, Times, serif" size="4">Anti-occupation resolution passes at union convention</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>CWA says bring troops home</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Amy Muldoon, CWA Local 1106</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 10, 2004 | Page 11</font> </P>

<P><font face="Times New Roman, Times, serif" size="3">THE NATIONAL convention of the Communication Workers of America (CWA) almost unanimously passed resolution August 31 that simultaneously endorses the "war on terror" but also calls for bringing the troops home from Iraq now. The original resolution was brought to the floor by the convention's official resolution committee, and called for more money for domestic security and training, a doubling of the number of Special Forces, and to "vigorously pursue the war on terror in conjunction with allies."</P>
<P>The leadership's resolution, called "Making Our Country More Secure," is largely a restatement of Democratic presidential candidate John Kerry's pro-war platform. While attacking Bush for preemptive war, it states that the U.S. "squandered" the world's goodwill after the September 11 attacks and the opportunity to build a more effective international military response to terrorism.</P>
<P>An amendment that called for bringing the troops home was proposed from the floor by Bill Henning, vice president of CWA Local 1180, which represents public sector workers in New York City. Not a single delegate took the floor to speak against the amendment, but 50 delegates--including veterans and family members of enlisted soldiers--lined up to speak in favor.</P>
<P>The CWA resolution follows similar statements passed earlier this year at the American Federation of State, County and Municipal Employees and the Service Employess International Union conventions. This shows that the conservative, Democratic Party line pushed by the leadership doesn't capture the depth of antiwar sentiment in the union.</P>
<P>The antiwar debate in the CWA, for example, has been pushed by rank-and-file activists for more than a year. Earlier this year, in CWA Local 1106, a Queens local at Verizon, debated a resolution calling for an end to the war and immediate withdrawal from Iraq.</P>
<P>The resolution was narrowly defeated after a shouting match at a union meeting--but members fought to keep the issue on the floor as a non-binding "recommendation." When some members claimed the issue was irrelevant to union members, other members shouted back about the potential of a draft, the cost of the occupation, and how George W. Bush's buddies in the Carlyle Group are trying to buy section of Verizon in upstate New York. </P>
<P>This was a big shift from even a year ago when the majority of the locals' members supported the invasion of Iraq. The "recommendation" came within five votes of passing, and even the executive board was split.</P>
<P>The debates over antiwar resolutions in the CWA and other unions shows that--despite the weight of Anybody But Bush politics sidelining criticisms to the left of Kerry--there's still room for real anti-war politics in the labor movement.</P>


