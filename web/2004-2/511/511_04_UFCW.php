<P><font face="Times New Roman, Times, serif" size="5"><b>UFCW has no plan to defend grocery workers</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 10, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I am disheartened, but not the least bit surprised, by the results of the United Food and Commercial Workers' (UFCW) latest contract battle with the grocery bosses ("Seattle-area grocery workers," August 20).</P>
<P>From the fight with Dominick's to the California strike to the Seattle contract, the union has weakened its stance for workers. After months of negotiations, reports of the settled contract included nothing about Safeway's profits, trends in health care cuts of workers compared to CEO salaries, or any such facts that might put the situation in perspective. </P>
<P>The union will now proceed to tell members to vote for John Kerry, "the workers' candidate." Well, what is Kerry going to do for workers that workers couldn't do for themselves? Absolutely nothing. </P>
<P>Darrin Hoop's article points to the not-quite-two-tier arrangement with health care. So basically the only gain this union made is a weak one. We should remember too that before negotiations even started, there was a huge amount of the pension plan lost, which will primarily affect the newer workers. It's not exactly two-tier, but again, it's a step in that direction. </P>
<P>All of the weaknesses we've seen with the UFCW negotiations go back to organizing. As an active member of Local 1105, I saw very minimal efforts in this direction. Some of this is due to inept leadership, but I also believe that some of this is by design. </P>
<P>During Madison Market's contract negotiations in 2003, Local 1105 flew in a representative from the international.  During the strike authorization vote, rather than explain how a strike would be organized, this representative (paid for by union members, mind you) proceeded to scare members by telling them how cold and difficult the picket line would be. </P>
<P>In short, the UFCW has no plan to improve rights of workers in the grocery industry except for maintaining the status quo. The direction of the union won't change until more workers tell the union officials what the Madison Market workers told them: We'll strike whether you like it or not.</P>
<B><P>Anonymous, </B>from the Internet</P>

