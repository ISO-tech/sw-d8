<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The cop and the groundskeeper</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 10, 2004 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THERE ARE two competing views of cops in the popular imagination. One has cops hanging around in their cars munching donuts and catching catnaps on cul-de-sacs, collecting free food and shaking down teenage drug dealers. If there's any danger, it's for those on the receiving end of their unchecked brutality.</P>
<P>The other view of cops, promoted by the media, is that they are good guys doing a hard, dangerous job--snatching the bad guys off the streets. They've got to wear Kevlar vests because bullets are whizzing past them all the time.</P>
<P>Indeed, if we were to guess by the portrayal of police work on television shows and in the news media, a cop's job is one of the most dangerous jobs in the world. What do the facts tell us?</P>
<P>According to the Bureau of Labor Statistics (BLS), in 2002, there were a total of 5,524 fatal work injuries in the United States. In the summary of the BLS's findings for 2002, cops don't even merit a mention.</P>
<P>Construction, factory, forestry, mining, fishing and trucking workers accounted for the majority of fatalities. In 2002, 1,121 construction workers died; 789 agricultural, forestry and fishing workers; 563 manufacturing workers; 121 miners; 910 transportation and public utility workers; 692 wholesale and retail trade workers; and 580 service workers.</P>
<P>Coming in second to last behind finance, insurance and real-estate workers (87 fatalities) are the police, with 106 deaths. Of the 106 police deaths, only 38 were from gunshot wounds. About that many people are shot and killed every year <I>by cops</I> in Detroit and New York City alone. The Stolen Lives Project, for example, compiled a list of 32 people killed by New York City and environs police in 1998.</P>
<P>Of course, this doesn't tell us about the workplace fatality <I>rate</I> (measured in deaths per 100,000 workers). That changes the picture, but not decisively. The 13 most dangerous jobs measured in terms of fatality rates, using 2002 statistics from the BLS, are as follows:</P>
<P>Alaskan crab fisherman: 400<BR>
Timber cutters: 118<BR>
Pilots and navigators: 69.8<BR>
Fishing industry overall: 71<BR>
Structural metal workers: 58.1<BR>
Driver-sales workers: 38.9<BR>
Roofers: 37<BR>
Electrical power installers: 32.5<BR>
Construction laborers: 27.7 <BR>
Truck drivers: 25<BR>
Mining: 23.5<BR>
Groundskeepers: 15<BR>
Police and detectives: 11.8 (Remember, only a fraction of this were actually homicides, i.e. cops being <I>murdered</I> on the job. Most were killed in car accidents.)</P>
<P>On average, there were four deaths per 100,000 workers across all categories. But most jobs requiring driving, strenuous physical work or operating machinery and equipment were considerably higher. </P>
<P>An Alaskan crabber is 34 times more likely to die than is a cop. The timber cutter's job is 10 times more dangerous than the cop's; the truck driver's job more than twice as dangerous as a policeman's.</P>
<P>Granted, a cop's got a more dangerous job than an office worker. But that's primarily because cops drive around, and any job that involves driving around all day (see the alarming statistic above for driver-sales workers, i.e. the guy delivering pizza and FedEx packages).</P>
<P>Moreover, if you include in the fatality rate only police who were killed on the job like we see in the movies, i.e. who were shot by "suspects," then the 11.8 rate drops to 4.2--just above the national average.</P>
<P>Sure, a cop's job is more dangerous than white-collar work--that much must be acknowledged. But even the groundskeeper has a tougher time on the job than Kojak. Keep your eyes open for the network premiere of "Groundskeeper 5-0."</P>


