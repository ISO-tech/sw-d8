<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 10, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Northeastern Illinois University</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Craig Althage</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--Faculty members at Northeastern Illinois University held a march of 150 around the campus August 31to demand that the administration return to the bargaining table. The faculty are members of University Professionals of Illinois (UPI), Illinois Federation of Teachers, Local 4100.</P>
<P>Their march--timed to coincide with the expiration of the old contract--was lively, as each of the marchers brought pots and pans to bang. The noise reached high decibels as students and university workers cheered during the faculty march through the campus. </P>
<P>The march was a challenge to the administration, which has dragged its feet in accepting the faculty's contract proposals. Instead of bargaining with the faculty union directly, the administration has appointed an insincere negotiator to mediate the contract. </P>
<P>The university's last proposal offered faculty a paltry 2 percent pay increase for each year of the contract and demanded a substantial increase in health insurance premiums. This is an outrage. </P>
<P>The average salary for full-time instructors and assistant professors at Northeastern is already less than the starting salary for Chicago Public School teachers. The university has also asked for changes in the contract language to weaken a civil rights provision that ensures hiring and firing free of racial bias. </P>
<P>Rightly, the UPI has rejected this offer. In the end, UPI let the administration know that they would be back--and in greater numbers. UPI activists are asking that students join them for pickets on September 7 and 14.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Seattle teachers</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Vikki Jambor, SEA</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEATTLE--On September 3, Seattle public-school teachers narrowly approved a new five-year labor contract by a vote of 388-349. Two thousand teachers, members of the Seattle Education Association (SEA), did not participate in the vote. </P>
<P>The new contract provides a1percent pay increase in the first two years and a 3 percent raise in the following two years. The last year's raise would be determined in 2009, and will be pegged to the highest teachers' pay in five of the surrounding districts--likely between 3 and 5 percent. </P>
<P>The other issue that's addressed in the contract is the achievement gap--the fact that students of color perform lower on standardized tests than white students. The contract states that more resources and time will be given to teachers to implement improvement strategies.</P>
<P>It also stipulates a hiring bonus for teachers that want to teach in lower performing schools. Steve Pulkkinen, SEA executive director and chief negotiator, called the deal a "really solid contract." </P>
<P>However, the close vote indicates some unhappiness with the contract. First, part-time employees will lose their full-time health insurance benefits--and health insurance costs can run up to $6,000 a year for teachers with families. In fact, Washington state teachers get paid on average about $8,800 less than surrounding states.</P>
<P>In addition, the new contract calls for an eight to one ratio student-teacher ratio in special education--an increase of two students. Further, the new contract maintains class sizes for mainstream students--even though, according to the National Education Association, Washington State would need about 11,000 more teachers to reach the national average in class size.</P>
<P>Finally, the five-year length of the contract has alarmed many teachers. Most districts have a one- to three-year contract. Teachers should stand up and fight against these attacks. The close vote shows the potential to organize rank and file fight back. </P>


