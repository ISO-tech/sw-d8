<P><font face="Times New Roman, Times, serif" size="4">Two Islamic charities are...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Innocent victims of a witch-hunt</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 10, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">MORE THAN two years ago, the federal government closed down two Chicago-area Islamic charities--the Benevolence International Foundation (BIF) and the Global Relief Foundation (GRF)--charging that the organizations "financed terrorists" and had ties to al-Qaeda. The Feds jailed, and then secretly deported Rabih Haddad, co-founder of the GRF, after an immigration judge declared him a "security risk." His wife and children were also forced out of the country.</P>
<P>Then, they forced Enaam Arnaout, the director of the BIF, into pleading guilty to diverting money to Islamic fighters in Bosnia and Chechnya--after prosecutors whipped up a lynch-mob atmosphere by implying that he aided terrorists, and then dropped all terrorism-related charges. </P>
<P>But now, even the commission that investigated September 11 has concluded that John Ashcroft and his goons were wrong--and that the government violated the civil rights of those who operated and supported the two charities. In a 152-page report released in August--over a weekend, to insure that few media outlets would pick up the story--the 9/11 commission questioned whether there is any link to terrorism in the two cases.</P>
<P>According to the report, there is "little compelling evidence that either of these charities actually provided financial support to al-Qaeda." Both cases raise "significant civil liberties concerns," the report said, because the laws used to freeze the charities' assets allow the government to act based on classified evidence, with only a limited review by U.S. courts.</P>
<P>Shutting down the BIF and GRF "came at considerable cost of negative public opinion in the Muslim and Arab communities, who contend that the government's destruction of these charities reflects bias and injustice with no measurable gain to national security," the commission concludes.</P>
<P>The report also notes that "despite unprecedented access to the U.S. and foreign records of these organizations, one of the world's most experienced and best terrorist prosecutors has not been able to make any criminal case against GRF and resolved the investigation of BIF without a conviction for support of terrorism." But don't expect that to stop John Ashcroft and his witch-hunters.</P>


