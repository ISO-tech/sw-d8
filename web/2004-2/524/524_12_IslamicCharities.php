<font face="Times New Roman, Times, serif" size="5"><b>Witch-hunt of Islamic charities</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 17, 2004 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">THE HYPOCRISY of the U.S. legal system knows no bounds.<p>
Last week, a federal magistrate ordered three Islamic charities and a man accused of raising money for the militant Palestinian group Hamas to pay $156 million in damages to the parents of David Boim, a U.S. teenager killed in the Israeli-occupied West Bank in 1996. Arlander Keys, the judge in the case, made headlines when he <i>tripled </i>the already outrageously high damages awarded by a federal jury.<p>
Boim's parents, who moved from the U.S. to Israel in 1985, filed their lawsuit under the Antiterrorism Act of 1990--a never-before-used law that allows any American who is a victim of terrorism abroad to sue for damages in U.S. courts.<p>
Last month, Keys ruled that the Islamic Association for Palestine and the Holy Land Foundation for Relief and Development, along with Mohammed Salah, were liable for David Boim's death--because they supposedly gave "material support" to Hamas in the form of fundraising. In early December, a jury added the Quranic Literacy Institute to the list of those responsible.<p>
But the trial was a sham from start to finish. The Quranic Literary Institute's lawyer wasn't given enough time to prepare a defense. He protested by not participating in jury selection, cross-examining witnesses, offering evidence or giving a closing argument. Lawyers for the other defendants also boycotted the trial--citing the impossibility of receiving a fair verdict.<p>
The three groups have already had their assets seized by the federal government and are under indictment on charges of supporting "terrorists." <p>
The Boims' lawyers claim that this latest award will set a precedent for shutting down "the domestic enablers of terrorism."<p>
But if that was true, then the parents of Rachel Corrie--the International Solidarity Movement activist who was murdered in 2003 when an Israeli bulldozer crushed her as she tried to prevent a Palestinian home from being demolished--should be allowed to sue the U.S. government for funding Israel's apartheid to the tune of more than $4 billion a year.<p>
And where is the justice for the innocent Palestinians killed in Israel's war? Like Iman al-Hams--a 13-year-old girl shot to death by Israeli soldiers in Gaza in October. A taped conversation of her killing has an Israeli commander telling his men--after firing at Iman with an automatic weapon--that "[a]nyone who's mobile, moving in the zone, even if it's a 3-year-old, needs to be killed."<p>
If the U.S. government really wanted to stop the "domestic enablers of terrorism," then allowing Palestinians to sue George W. Bush and Congress is the place to start.<p>

