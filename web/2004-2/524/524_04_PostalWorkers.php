<font face="Times New Roman, Times, serif" size="5"><b>Postal workers take action</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 17, 2004 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
This past October, employees at the Times Square Post Office in midtown Manhattan became fed up with the condescending attitude and intimidation tactics meted out by Acting Station Manager Ron Hart. Hart is notorious in the New York District for callous labor practices and hostile work floor behavior--in one instance, allegedly contributing to the death of a city carrier. <p>
After mounting problems, the officers of Branch 36 of the National Association of Letter Carriers (NALC) stated we would be fired for any serious wildcat action against Hart, and that the union would not help us. We were told to go through the lengthy grievance process individually on an abuse-by-abuse basis, to elect stewards who will fight for us, that we had good jobs and overtime, and that solidarity didn't exist anymore. We were told that petitioning wouldn't matter, or would probably backfire and bring reprisals. <p>
However, Times Square workers decided to take matters into their own hands and collectively organized to petition for Hart's removal. Nearly half of the station's carriers signed on, and Hart quickly disappeared from the work floor. A new manager was appointed a short time later.<p>
In the new "e-mail economy," postal workers are routinely reminded by both our union and management that the United States Postal Service (USPS) is "undergoing sweeping regulatory transformation," and that unless we boost productivity and generate new sales leads, falling mail volume and competition with private firms leaves our future employment in doubt. <p>
Besides employing highly paid scum like Ron Hart to harass workers, the USPS spends tens of millions of dollars on supervisor bonuses, management inspection teams and worker efficiency software, while outsourcing jobs to low-paid nonunion private presort companies who profit from USPS-developed technology. Such mismanagement and cutbacks result in terrible service to the public, which is then used as a pretext for more austerity. <p>
In addition to having debilitating and stressful jobs, postal workers were the first victims of the anthrax mailings and have been killed and maimed in the current war for oil in Iraq, where the mail service is privatized and the U.S. enforces Saddam-era anti-union laws. <p>
The U.S. Office of Special Counsel further weakened us by ending the anti-discrimination status of sexual orientation for all federal workers. <p>
Congress recently joined in the attack by raiding the civil service pension for the second time to pay for their continued colonization of Iraq. Adjusted for inflation, letter carriers' wages have fallen 4 percent since 1978 as productivity has increased 11 percent. <p>
We need shop-floor leaders reminding us that 9 percent of the U.S. economy passes directly through the USPS, and that, with over 700,000 employees, we are the largest public-sector workforce in the country (second overall only to Wal-Mart) and one of the largest unionized workforces in the world. <p>
It is time for NALC members to join their sisters and brothers in the American Postal Workers Union and the National Mail Handlers Union in endorsing a resolution against this terrible war and form a political strategy that goes beyond business unionism and bribing politicians who get 100 times as much from their corporate masters. <p>
Our stand against Ron Hart contains the germ of how our largest gains were won: the illegal wildcat strike in 1970, the first ever waged by federal workers. It is the militant spirit of that struggle, organized by the rank and file, pressuring the union and paralyzing the company that points the way to a different kind of "transformation," where USPS becomes an employee-owned and democratically self-managed enterprise for the benefit not of Ron Hart, management, Pitney Bowes, MasterCard, Verizon or Macy's, but for the U.S. public and the USPS' own employees. <p>
They need us. We don't need them.<br>
<b>Frank Couget,</b> NALC Branch 36, New York City<p>

