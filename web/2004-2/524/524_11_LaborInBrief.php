<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">December 17, 2004 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Madison Market Co-op</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jorge Torres, Darrin Hoop and Madison Dyke, UFCW Local 1105</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--Contract negotiations between Madison Market Co-op and the 80 grocery workers of United Food and Commercial Workers Local 1105 are set to begin in early January.<p>
During the last contract fight more than two years ago, the union won significant increases in wages and health care, despite the fact that management hired anti-union consultant Bob Braun in an attempt to force through a concessionary contract.<p>
The current management team of General Manager Reese Williams, Store Manager Eric Thomas and Human Resources Director Gavin Morgan has not learned the lessons of their predecessors. In an attempt to intimidate us, they've launched a campaign of harassment. Management has cut back hours, handed out verbal warnings for "violations" of a new attendance policy and suspended three employees in the last two months. <p>
The most egregious case of harassment involves one of the current union negotiating team members, Suzie True, who was pulled off the shop floor and threatened with termination if she didn't immediately submit to a drug test. There are no provisions in the contract that allow for employee drug testing. And True was denied union representation. Later, she was suspended for six days and barred from the store pending the results of her test, which later came back negative.<p>
Since then, workers have met to discuss strategies to stand up to management. A petition is being circulated calling for Thomas' separation from Madison Market. How we respond to these abuses will be key to winning a good contract.<p>
Some of our demands include higher wages, paid maternity/paternity leave, stronger seniority language, better health care and the democratic election of management. In the run-up to the last contract fight, workers organized a contract campaign, which included frequent workers' meetings to discuss our contract demands and how to win them. Workers handed out flyers and circulated petitions to educate each other and mobilize customer support.<p>
Ultimately, it took a nearly unanimous strike authorization vote to force management to concede to our demands. Continuing to build solidarity will be key to victory this time around.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Chicago City Colleges</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By a CCCTU member</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--The teachers and professionals of the Chicago City Colleges (CCC) are learning that having won a contract after their successful three-week strike is just the first part of their battle.<p>
Represented by the Cook County College Teachers Union, Local 1600 of the American Federation of Teachers, the teachers fought draconian demands that would have busted their union and severely eroded their livelihoods.<p>
Solidarity was the key. Out of 775 members, only seven crossed the picket line. And dozens of part-time teachers--represented by a different union--also honored the picket lines, as well as retired teachers and students.<p>
Therefore, the CCC central administration--led by its politically connected chancellor, Wayne Watson--is attacking our supporters despite the renewal of the "no reprisals clause" in our contract that states that no one would be penalized for supporting the strike.<p>
At Daley College, the administration had to abandon disciplinary hearings against part-time teachers after angry faculty union members confronted her. At Truman College, part-time teachers and two AFSCME-represented Adult Education teachers faced similar disciplinary hearings that were met with lively protest.<p>
More seriously, Ben Rubin, a full-time teacher at Malcolm X College, was arrested at his home in front of his family two weeks after the strike had ended. Rubin and a student from Malcolm X face trumped-up assault charges following a protest held at CCC headquarters near the end of the strike.<p>
Even more vile is the case of a Nigerian student who will be facing deportation hearings because Wright College officials had him arrested for distributing pro-strike fliers.<p>
Department chairs are being provided with blacklists of part-timers and warned not to hire them again.<p>
Students have responded by forming a new group, Students for Public Education. The faculty union's response, however, has been largely limited to legal actions--but we need more. Any more delays in taking resolute joint action will just embolden Watson and his cronies.<p>

