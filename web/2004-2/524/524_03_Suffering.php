<font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Their suffering is not an accident</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 17, 2004 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">ALMOST HALF of the world's 2.2 billion children live in poverty. One in six children on the planet are severely hungry, while one of every four kids under age five are severely or moderately underweight.<p>
One in five has no safe water--and one in three has no toilet or sanitation facilities at home. Nearly half a million children died of AIDS last year, and more than two million children worldwide have been infected with HIV. <p>
These statistics, from UNICEF's annual <i>State of the World's Children </i>report, show that more than a billion children suffer extreme deprivation because of poverty, HIV/AIDS or war.<p>
All of these catastrophes are the result of a system that places profits above human needs. As UNICEF Executive Director Carol Bellamy said, "Poverty doesn't come from nowhere; war doesn't emerge from nothing; AIDS doesn't spread by choice of its own. These are our choices."<p>
In particular, the wealthy and powerful elite that rules rule the U.S. should be made to answer for their "choices."<p>
Choices like making sure that generic AIDS drugs aren't distributed in poor countries--in order to keep the already super-inflated profits of U.S. pharmaceutical giants high. According to UNICEF, the lack of cheap, available AIDS drugs meant that from 2001 to 2003, the number of children orphaned by AIDS soared from 11.5 million to 15 million. Some 80 percent of them are in Africa.<p>
Then there's choice of the U.S. government to be stingy with aid to developing nations--and to punish poor countries that deviate from Washington's line. U.S. sanctions on Iraq, for example, meant that mortality rates of children under 5 more than doubled in Iraq--from 50 per 1,000 in 1990 to 125 per 1,000 in 2002. <p>
And the U.S.-led drive to war and empire is deadliest for the young. Nearly half of the 3.6 million people killed in war since 1990 have been children.<p>
The resources exist to eliminate these horrors. According to UNICEF, the annual cost to reduce extreme poverty by 50 percent by 2015 is between $40 billion and $70 billion. World military spending topped out at $956 billion in 2003--with the U.S. accounting for more than $400 billion of this by itself.<p>
The new report makes it clear: Poverty, disease and war don't have to be the reality for the world's children. But as long as a system of profit and exploitation remains, these outrages will continue.<p>

