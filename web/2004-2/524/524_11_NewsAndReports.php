<font face="Times New Roman, Times, serif" size="4"><b>Housing is a human right</b></font><p>

<font face="Times New Roman, Times, serif" size="3">MADISON, Wis.--With winter coming to Wisconsin, members of the Madison Warming Center Campaign held a December 9 sit-in at Mayor David Cieslewicz's office to demand immediate emergency shelter relief for the homeless. <p>
Conservative estimates set the number of homeless people at 2,000 people per night, but there are only 300 shelter beds. While the city will spend millions on a new park and an art center, it plans to spend exactly nothing to guarantee the basic human right to shelter. <p>
This blatant hypocrisy led nearly 75 people to occupy the reception area of the mayor's office and the hallway out front. As the mayor left his office, protesters shouted him down with chants such as "Hey Dave, how do you sleep? Homeless kids are on the streets!" More than 20 protesters occupied the hallway of the government building for the night. <p>
Protests continued the next day, which landed on International Human Rights Day, while activists spread the word about the ongoing occupation of the mayor's office and the conditions faced by Madison's homeless.<p>
After the building closed on the second day, the police waited for those protesters who did not intend to stay the night to leave before moving in to make arrests. <p>
This week, the mayor has invited us to a meeting that will present a perfect opportunity to go on the offensive. We must make sure that the meeting is open to the public and to show up in full force. At the same time, we must continue the picket outside of his office and let him know that human rights are non-negotiable.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Defend academic freedom at NYU</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Wrigley-Field</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--In response to recent witch-hunt against Joseph Massad and other pro-Palestinian professors at Columbia University, Students for Justice in Palestine at New York University (NYU) organized a panel on academic freedom on December 8. The event, which was also sponsored by the Campus Antiwar Network, turned out 110 people who came to hear professors from several universities speak out against the attacks on professors' ability to teach views critical of Israel and U.S. foreign policy.<p>
NYU professor Khaled Fahmy explained why he came to speak. "I wanted to highlight how the charge of anti-Semitism is used in a cynical way to silence any critique of Israel, and how someone like Joseph Massad has genuinely and consistently always opposed anti-Semitism," Fahmy said.<p>
Several professors spoke about how the attacks at Columbia relate to attacks on their own campuses. Amiel Alcalay, who teaches in the City University system, spoke about a new Homeland Security Management Institute at Borough of Manhattan Community College, which preys on the lack of job opportunities for working-class students.<p>
Event organizers were pleased with the turnout. "It gives us the energy to continue organizing events," Rania Jawad of Students for Justice in Palestine told <i>Socialist Worker.</i> <p>

