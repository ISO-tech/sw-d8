<font face="Times New Roman, Times, serif" size="4">White House wheels out its right-wing wish list</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>What will it take to stop Bush?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 17, 2004 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THE BUSH gang has big plans for 2005. So our side better be ready to take a stand in the New Year.<p>
The Republicans are using the claim that they won a mandate in the election to wheel out every make-the-rich-richer scheme they've got. On unpopular issues like privatization of Social Security and reform of the tax system, the question for Republicans isn't if, but when.<p>
Neanderthal House Majority Leader Tom DeLay wants to start by sticking it to working people again with tax reform--making George Bush's enormous tax cuts for the rich permanent and possibly eliminating the federal income tax in favor of a regressive national sales tax.<p>
But Bush's people are determined to push through their Social Security giveaway to Wall Street first. Their scheme would steal $1 trillion from the money used for our retirement system and gamble it in the stock market--while forcing a cut in benefits for seniors.<p>
Don't count on the Democrats to be a genuine opposition. Their "strategy" is deciding how far they'll cave.<p>
Bush is packing his second-term Cabinet with assorted thugs and grifters, but not a single Senate Democrat has even threatened to oppose any of their confirmations. <p>
Not even Bernard Kerik. In fact, two of the biggest supporters of his nomination as chief of the Department of Homeland Security were New York's Democratic Senators Hillary Clinton and Charles Schumer.<p>
But Kerik won't get the job because he didn't pay taxes for an immigrant nanny that he employed. Actually, the real skeletons in Kerik's closet were the thousands of dollars in gifts from a mob-connected business and profiteering from companies that have contracts with the Homeland Security department.<p>
Absurdly, White House officials irritated by this bump in the road are more publicly critical of Kerik today than Clinton and Schumer.<p>
The Democrats' surrender was further illustrated by the passage of White House-backed legislation that revamps U.S. intelligence services. The new bill gives the FBI and federal law enforcement even more powers, increases the number of full-time border patrol agents and immigration investigators, opens new detention facilities for violators of immigration law and sets national standards for issuing birth certificates and driver licenses. <p>
Yet the main opposition wasn't from Democrats determined to defend civil liberties--but from Republicans who complained that the legislation didn't go far enough.<p>
The administration's claim that it has an election mandate for this onslaught is so much bluster designed to further disarm the Democrats. In reality, the political shift in November was nowhere near as big as, for example, the 1994 Republican Revolution, when the GOP won a majority in both the Senate and House for the first time in 40 years.<p>
But there <i>was</i> a shift. Unlike 2000, Bush actually won the 2004 election, including the popular vote. Republicans made gains in their control of both the House and Senate, and conservatives won a series of right-wing ballot measures.<p>
The point to remember is that the Republican victory wasn't the result of their overwhelming strength, but the weakness of their opposition.<p>
John Kerry ran a pathetic campaign that tried to out-Bush the Bush campaign on everything from the "war on terror" to the economy. He lost because he didn't give people any reason to vote <i>for </i>him. Nationally, the Democrats hid their heads, hoping that the widespread hatred for Bush would be enough to win--and they refused to oppose appalling right-wing attacks like the referendums to ban gay marriage.<p>
None of this bodes well for the Democrats' future. But progressives have to stop worrying about that--and focus instead on building real resistance to Bush.<p>
The potential for a fightback won't be reflected in the mainstream media--which has gone into lapdog mode to please the Republican masters of Washington. But the ingredients for building an opposition exist--in workplaces, campuses and communities around the country.<p>
As always, these struggles will start small--but they can grow, gather attention and make wider links very quickly if they reflect the discontent that the bipartisan establishment in Washington ignores.<p>
The overriding issue is Washington's savage war in Iraq. The U.S. isn't winning the occupation any more today than before the election, and the casualty rate among U.S. soldiers will only grow. Bush's plans for Iraq and elsewhere in the world require more military forces, which means the potential of a new draft--an explosive political issue that Washington may not able to avoid as it wages a war on the world.<p>
From the war to abortion rights to the attack on Social Security, Bush's right-wing offensive will provoke opposition and resistance.<p>
It's crucial to be involved in all of the struggles as they emerge--not only in organizing activities, but participating in the debates about how to fight and answering questions about the wider questions they raise. That's the way to rebuild a left that can stand up to the Bush agenda.<p>

