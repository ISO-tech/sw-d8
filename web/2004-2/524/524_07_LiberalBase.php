<font face="Times New Roman, Times, serif" size="5"><b>Liberal groups that shill for Democrats</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 17, 2004 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">WHEN POLITICAL analysts talk about the Democratic Party's "base," they generally mean groups like women, gays and lesbians, and union workers, mobilized through traditional liberal organizations like the AFL-CIO or the National Organization for Women (NOW).<p>
With the Bush administration preparing to attack abortion, Social Security, gay marriage and a number of other issues that these groups defend, it's doubtful that they have the wherewithal to mobilize the kind of resistance that will be needed to stop the right.<p>
That's because many of the traditional liberal interest groups have become little more than Washington-based shells with no real grassroots base. They operate as Democratic lobbies that tailor their "message" to the party's talking points of the day. They are part of the inept "Washington insider" Democratic establishment of politicians, consultants, pollsters and lobbyists.<p>
A good example of what this means came when Sen. Harry Reid of Nevada, a conservative, anti-abortion politician, took over as Senate Minority Leader in the wake of the defeat of former Senate Minority Leader Tom Daschle. NARAL Pro-Choice America and NOW raised no objections to Reid's promotion. In fact, Reid even touted his support from women senators and from Kate Michelman, former president of NARAL.<p>
With the supposed leaders of the pro-choice movement playing this "insider" game, it's no wonder that they seem incapable of inspiring the pro-choice majority to make its voice heard. <p>
Their strategic vision, like that of their friends in the Democratic Party, is fixated on going backwards less rapidly than on trying to shift the political climate in their favor. Having accepted that the best they can hope for is "the lesser of two evils"--and having become part of the Washington apparatus that promotes lesser evilism--it's not surprising that these supposed Democratic "base" organizations would move rightward after the election. <p>
But they're turning a setback into a full-scale retreat. <p>
The Human Rights Campaign, the main gay/lesbian/bisexual/transgender lobby tied to the Democratic Party, concluded at a December meeting that it needed to "moderate its message and its goals," the <i>New York Times </i>reported December 9. "One official said the group would consider supporting President Bush's efforts to privatize Social Security partly in exchange for the right of gay partners to receive benefits under the program."<p>
So in addition to retreating on fighting for equal marriage in favor of emphasizing building personal relationships, some in the HRC are willing to throw the elderly and disabled overboard if only the Republicans would be nicer to them. If the HRC follows through on this plan, it will amount to a surrender to the Republicans and the right. <p>
As Matt Foreman, director of the National Gay and Lesbian Tax Force, told the <i>New York Times</i>, "A lot of gay people understand the concept of bullies. The worst thing you can do with a bully is not fight back because you'll only get hit harder the next day." <p>
The HRC's post-election moves aren't an adjustment to political reality. They're a confession of political bankruptcy.<p>
Meanwhile, organized labor still has a mass base, but it faces increasing pressure from hostile employers and the government. What will it do?<p>
A battle is shaping up to take place at the 2005 AFL-CIO convention between forces arrayed in the New Unity Partnership--which advocates consolidation of multiple unions into about 20 and dedication of substantial resources to organizing--against the <i>status quo</i> leadership of federation President John Sweeney. <p>
Clearly, unions must increase their membership if the employers and politicians are going to take them at all seriously. But it's unclear if either side in the debate holds the key to stemming union decline. And a union movement that continues to muddle along will simply decline further.<p>
All of the major reforms that have improved the lives of working people and the oppressed have come through struggle. And one of the lessons of these battles is that organizations that don't or won't rise to the struggle are pushed aside in favor of those that will fight. The next few years might provide more examples of this historical lesson.<p>

