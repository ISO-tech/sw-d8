<font face="Times New Roman, Times, serif" size="4">An emergency appeal from Argentina</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Defend the Zanon workers</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 17, 2004 | Page 5</font><p>

<font face="Times New Roman, Times, serif" size="3">THREE YEARS ago this month, millions of Argentines took to the streets in mass demonstrations that paralyzed the country and threw out a succession of presidents. Spearheading the revolt against the hated government's free-market austerity measures were organizations of Argentinian workers, especially the unemployed.<p>
In an open challenge to the country's rulers, workers took over dozens of factories closed down by employers--and began operating the workplaces under their control.<p>
Ever since, the government has been trying to undermine these factories. Currently, it is going after the Zanon ceramic tile plant, which was featured in the recent documentary <i>The Take, </i>made by author and global justice activist NAOMI KLEIN and left-wing journalist AVI LEWIS. Here, Klein and Lewis make an emergency appeal to support the Zanon workers.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear Friends,</b> <br>
We're writing to ask your help in defending an inspiring and courageous workers' struggle in Argentina. The Zanon ceramic tile factory, a democratic, worker-run factory in Patagonia, is facing a serious threat of eviction, and the workers have asked us to gather international support for their struggle.<p>
For those of you who have seen our documentary, <i>The Take, </i>the Zanon factory and Argentina's wider movement of worker-run companies will be very familiar.<p>
For those of you who haven't, this new movement of some 15,000 workers in almost 200 democratic workplaces is building hope and a concrete economic alternative in the rubble of Argentina's disastrous experiment with orthodox neoliberalism in the 1990s.<p>
Recovered companies are run by assembly: one worker, one vote. In most of them, workers have decided that everyone should receive the same salary. They are proving the viability of an economy run on an entirely different value system, and they are growing.<p>
In the past year, Zanon has increased its workforce from 300 to 450--a 50 percent increase. What multinational corporation or national government could boast of such a dramatic rise in decent-paying employment in the middle of an economic crisis?<p>
And Zanon has cultivated a deep and mutual relationship with the surrounding community. For 20 years, the poor neighborhood of Nueva Espa&ntilde;a, across the highway from the factory, has been asking the provincial government for a health clinic. Zanon workers took a vote earlier this year, and in three months built and opened a brand new community health facility.<p>
But now the provincial government is threatening to send in the Gendarmeria to remove Zanon's precious machines. This is an illegal order, since this force is federal, intended to police Argentina's borders. On a second front, the federal judge presiding over the bankruptcy of the former owner is refusing to recognize the Zanon workers' cooperative (called FaSinPat--short for "Fabricas Sin Patrones," or Factories Without Bosses).<p>
The former owner received millions in public subsidies and still amassed a huge debt and bankruptcy. He has since been removed from his own board of directors for "accounting irregularities." <p>
The workers' cooperative, on the other hand, is a major success. It is now producing 380,000 square meters of ceramic tiles a month--a level of production higher than when the former owner closed the factory--and the workers do it without the huge public subsidies (300,000 pesos per month) that he used to receive.<p>
The Zanon workers have told us that a massive international petition in support of their struggle could make a key difference with the various levels of courts and governments.<p>
Zanon's highly successful combination of direct action and direct democracy is a precious example of that other world that is possible--that is growing before our very eyes. We urge you to sign the petition--at www.petitiononline.com/zanon/petition.html --and do everything you can to encourage others to do the same.<p>
Thank you for your time and support!<br>
<b>Avi Lewis and Naomi Klein</b> <p>

