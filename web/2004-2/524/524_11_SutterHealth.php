<font face="Times New Roman, Times, serif" size="5"><b>SF nurses gear up to fight understaffing </b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jessie Muldoon</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 17, 2004 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">OAKLAND, Calif.--Hospital workers and nurses at Sutter Health returned to work December 6 following a one-day strike and a subsequent lockout. <p>
The five-day lockout of 7,000 workers, members of the Service Employees International Union (SEIU) Locals 250 and 707 and the California Nurses Association (CNA) was retaliation for the December 2 strike. SEIU--which is fighting for a new contract--and the CNA picketed and rallied throughout the duration of the lockout. <p>
Unfortunately, the looming threat of the lockout undermined some workers' confidence. At Alta Bates Hospital in Berkeley, only about 60 percent of the union workers struck.<p>
The key issue is patient care and understaffing, which affects the quality of health care. According to a law pushed by the CNA, the state of California must implement a 1-to-5 nurse-to-patient ratio as of January 1, 2005--compared to the current ratio of 1-to-6.<p>
But in November, Gov. Arnold Schwarzenegger unilaterally postponed implementing the law and launched a media blitz opposing it as too costly. Schwarzenegger then insulted health care workers at a high-priced fundraiser in front of 10,000 people at the California Governor's Conference on Women and Children. When CNA members unfurled a banner during the governor's speech and chanted, Schwarzenegger dismissed them as "special interests." <p>
Now the nurses' union is pursuing a lawsuit to force the governor to implement the staffing law.<p>
As in the case of the hotels in San Francisco, locking out union workers is a powerful fear tactic that management is more than willing to employ. <p>
However, a positive development that has come out of this struggle is a new willingness of SEIU and CNA to work together. CNA and SEIU have had numerous conflicts over the course of a decade regarding jurisdiction and other issues. At the height of the dispute, each union bashed the other in their publications and even refused to honor picket lines.<p>
The thaw in the relationship is an important step for the unions' ability to take on the hospital chains. But the hospital bosses are raising the stakes. The unions--if they are going to pose a real challenge--will have to rise to the occasion.<p>

