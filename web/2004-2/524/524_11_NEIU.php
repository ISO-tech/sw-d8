<font face="Times New Roman, Times, serif" size="4">Union defends work conditions and gets pay raises</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>NEIU strike wins gains</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By a UPI member</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 17, 2004 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--After 20 days on the picket line, the faculty and staff represented by the University Professionals of Illinois (UPI) at Northeastern Illinois University (NEIU) won a clear-cut victory and voted overwhelmingly December 8 to accept a four-year contract. The ratification meeting was a celebration of both the contract and the solidarity built in our struggle.<p>
The deal includes a 3.5 percent across-the-board pay increase as well as extra money to raise the lowest paid faculty and staff salaries and to begin addressing pay inequities in each of the four years of the contract. <p>
The new contract also includes much of the language desired by the membership to protect workload and prevent the assignment of unpaid duties. Domestic partner benefits and non-retaliation language to protect student supporters were also achieved in the agreement. <p>
While we didn't get everything that we wanted in the contract, we did gain more than expected and far more than was offered before the strike.<p>
The membership is most enthusiastic about our most important gain--the solidarity of the membership and our renewed sense of pride and respect. The day after the ratification vote, faculty and staff returned to campus exhilarated by the strike experience and ready to continue the struggle to protect the quality of education on campus and to continue building the strength of our union. Solidarity and congratulatory messages continue to arrive--and on-campus victory party is planned for December 17 to bring the picket line spirit onto the campus.<p>
The victory at NEIU was the result of more than a year of democratic organizing within the union, which is part of Local 4100 of the American Federation of Teachers. The debates inside our union continued during the strike. Some of us criticized our union president's decision to carry out secret negotiations mediated by two Democratic politicians--talks that excluded our elected bargaining team.<p>
Nevertheless, the efforts to revive union activism paid off in solid picket lines and a membership determined to protect the quality of our students' education and our own livelihoods.<p>
The impact of this victory was felt on another working-class campus on the South Side of Chicago where Chicago State University, another UPI campus, ratified a good contract on December 13. The union negotiator there wrote to thank the NEIU campus, noting that it was no coincidence that their agreement was reached as our strike ended.<p>
While we continue the celebration of our victory we also continue to plan and organize for the ongoing struggle to demand fair funding for our campus and others. And we do so with a membership and student body transformed by our experiences on the picket line.<p>
The future of decent living standards for those of us working on university campuses and of the quality of education available to our students will be determined by the willingness of faculty, staff and students to stand together and demand what is ours. We have won an important battle at NEIU and are committed to continuing the struggle for fair wages, good working conditions and quality education.<p>

