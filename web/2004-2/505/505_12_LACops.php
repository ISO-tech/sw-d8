<P><font face="Times New Roman, Times, serif" size="4">Beating caught on videotape</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Brutal LA cops strike again</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Danielle Heck</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 2, 2004 | Page 12</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WATCHING THE grainy video images of the police beating of Stanley Miller last week, Los Angeles residents couldn't help but be reminded of 1991, when cops beat another Black man on camera&#150;Rodney King.</P>
<P>People may have been sickened by the footage of officers hitting Miller 11 times with a flashlight after the alleged car-theft suspect had already surrendered to police. But anyone who has had dealings with the LAPD&#150;particularly if they're Black or brown&#150;wasn't surprised.</P>
<P>Mayor James Hahn and Police Chief William Bratton are worried that the recent beating of Miller could spark something similar to the 1992 Los Angeles Rebellion, the riots that erupted after officers in the King beating were acquitted of criminal charges. Since then, the exposure of drug dealing, frame-ups and shootings of innocent people in LA's Ramparts division in the late 1990s has added more crimes to the LAPD's long rap sheet.</P>
<P>The 36-year-old Miller was brutally beaten with a flashlight by an LAPD cop in the early morning of June 23. The problem for police is that the beating was caught on videotape by two helicopter television news crews&#150;and aired on TV.</P>
<P>Even top police department officials described the beating as "Rodney King-esque." The videotape shows the unarmed Miller, who is Black, surrendering after a car chase that ended in Compton.</P>
<P>After he sprints a short distance from the car, he raises his arms and starts to crouch, appearing to lie down. Two officers are seen restraining Miller on the ground, while a third, John Hatfield, runs up to Miller. He first kicks Miller&#150;and then brutally hits him 11 times with a metal flashlight. </P>
<P>Incredibly, Los Angeles police are <I>allowed</I> to use metal flashlights to strike suspects. This was apparently news to Los Angeles Police Chief William Bratton, who made his career as head of the Boston and New York City Police Departments. As Bratton put it, "The image of the flashlight looks problematic. It looks awful quite frankly."</P>
<P>Hatfield, the officer who delivered the blows, is now claiming that another officer yelled that the man was armed with a gun. The object they say one officer mistook for a gun turned out to be wire cutters, which were found in the suspect's right front pants pocket.</P>
<P>Officers also said they were concerned Miller was concealing something in his hand, possibly a weapon. It turns out Miller was clenching money: a $5 bill and three $1 bills.</P>
<P>About 100 people gathered on short notice June 25 outside the LAPD's 77th Street Division station to protest. They chanted, "No justice, no peace," and held up posters, including one that read, "How many more beatings?" Another rally of 100 people was held on June 28 with the Rev. Al Sharpton, among other activists.</P>
<P>We must send a message to the police that we won't allow their abuse without consequences. We have to start building organizations that involve the people most affected by police terror to stop this brutality.</P>


