<p><a name="G8"></a><font face="Times New Roman, Times, serif" size="4"><b>Turning Georgia into a police state</b></font> </P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Imagine 40 police cruisers, four Federal Protective Service vans, 22 police officers mounted on horses, 20 on motorcycles, two on bicycles, 12 U.S. Army foot soldiers, two military Humvees, about 20 riot cops and a helicopter. Now imagine them protecting a Starbucks franchise from a group of 30 anti-globalization activists with bandanas and drums, marching on the sidewalks. </P>
<P>Welcome to Savannah, Ga., during the 2004 Group of Eight (G8) Summit. The thought that this much security can be deployed and used against protesters under the guise of fighting terrorism is a truly scary one.</P>
<P>I drove down to coastal Georgia with some friends to protest corporate globalization, the war in Iraq, the war on terror, Bush's proposed Greater Middle East Initiative, and all of the other human and environmental abuses that G8 sets in motion. This year's summit took place on Sea Island, an upper-class island off the southern coast of Georgia.</P>
<P>Security was organized by the Secret Service and included roughly 20,000 military, federal and state law enforcement officers to protect the eight world leaders against terrorism. During the police and military occupation of Brunswick and Savannah, public schools were closed and local businesses suffered. But in the end, it was all for the "public good." You never can be too safe when dealing with the security of our homeland, right?</P>
<P>Only a few hundred other protesters joined me and my friends in Georgia, instead of the 70,000 that some news reports predicted just weeks ago. Why such a low turnout? A variety of reasons. As one of the organizers in Brunswick explained, "The city refused to give us a permit. We just got this space last weekend."</P>
<P>Protesters in Savannah blame the small turnout on threats of arrest. The police presence created an atmosphere of submission that discouraged dissent. People decided not to attend the protests because they didn't want to have to deal with the sheer numbers of "anti-terrorist" forces that they would encounter. They didn't want to have to deal with being in a police state.</P>
<P>Later this summer, Boston and New York City, too, will be occupied by the police in attempt to "curb terrorism" at the Democratic and Republican National Conventions. The sheer numbers of anti-terrorist/anti-activist forces that massed in coastal Georgia during the G8 Summit shows that now the president has the power to cry "Terrorist!" and turn a city into a police state. </P>
<B><P>Micah Lee, </B>From the Internet</P>

