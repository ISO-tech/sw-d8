<p><a name="Soldier"></a><font face="Times New Roman, Times, serif" size="4"><b>Military employees let loose at Bush</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Recently, in a bar in Great Falls, Mont., I got into a conversation with a veteran of the U.S. Navy and three military construction workers stationed nearby at Malmstrom Air Force Base. When the topic of the war came up, they all said immediately that it was about oil and profit. </P>
<P>Only one seemed to think it was even remotely possible that weapons of mass destruction would be found in Iraq, and when he added that Bush was a liar anyway, everyone sitting at our table burst out with enthusiastic agreement. </P>
<P>The fact that John Kerry's Web site says he wants to deploy 40,000 more troops was news to these soldiers, however, and they did seem to retain some faith in the idea of voting against Bush. "It's all about electing the right liar," one of them explained.</P>
<P>Tom Barton, editor of <I>GI Special</I>, has said that among Marines returning from combat, such disgust with the war would hardly even be newsworthy, but that among Air Force personnel in a place so far away from Iraq, it hints that antiwar sentiment in the armed forces is reaching new heights.</P>
<B><P>Alan Wallis, </B>New York City</P>

