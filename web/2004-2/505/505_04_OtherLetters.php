<P><font face="Times New Roman, Times, serif" size="4"><b>Conn. says good riddance to a union-buster</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Connecticut Gov. John Rowland, a Republican, announced his resignation last week after a State Supreme Court decision was handed down that would have required him to testify before a committee investigating possible impeachment. Rowland was facing federal corruption charges surrounding his acceptance of gifts from friends, state contractors and state employees. </P>
<P>While Rowland renovated his summer home and enjoyed thousands of dollars of Cuban cigars and fancy champagne on the taxpayer dollar, Connecticut's residents paid the price. Rowland presides over the richest state in the country, but it includes four of the poorest cities. During his tenure, he's only exacerbated that divide by slashing programs for the poor and enacting tax cuts for the rich.</P>
<P>An avid George W. Bush campaigner, Rowland has leveled vicious attacks on unions, including threatening to "reopen" state workers' contracts to exact more cuts last year and even illegally using state funds to hire strikebreakers during a strike by employees of private nursing homes.</P>
<P>Perhaps nothing better exemplified Rowland's utter contempt for those in need than his visit to New Haven in 2002, when a tent city had been constructed on the public green due to the closure of a homeless shelter. Rowland deemed it an "eyesore" and ordered city officials to close it down.</P>
<P>We say good riddance to Rowland, not only because of his blatant corruption but also for his consistent attack on ordinary people in the interests of the rich and powerful--by both legal and illegal means.</P>
<B><P>Robin Hodges and Leela Yellesetty, </B>New Haven, Conn.</P>

