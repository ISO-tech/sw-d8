<P><font face="Times New Roman, Times, serif" size="4">U.S. official on the puppet prime minister of Iraq: "He's our thug"</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bush's sham "handover"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 2, 2004 | Page 1</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"THE IRAQI people have their country back." So declared a smug George W. Bush earlier this week after a surprise "handover of power" in Iraq.</P>
<P>If you blinked at the wrong time, you missed the whole thing. That's because U.S. officials were so scared of the coordinated resistance attacks across Iraq--more than 100 people were killed in a series of explosions in five Iraqi cities in one 24-hour period last week--that they secretly pushed the transfer date up by a couple days.</P>
<P>Instead of the expected pomp, circumstance and hot air, reporters got a lackluster press conference held in a tightly guarded room. Only about 30 people were present for what Iraq's new Prime Minister Iyad Allawi--a handpicked U.S. stooge with CIA ties--called an "historic" day.</P>
<P>U.S. overseer Paul Bremer handed Allawi a letter transferring "authority"--before hopping a plane and high-tailing it out of the country. Even the Bush administration's lackeys couldn't keep a straight face when claiming that Allawi will lead Iraq towards "democracy."</P>
<P>As one senior diplomat told the <I>Los Angeles Times</I>, "He's a thug, but he's our thug." Allawi himself wasted no time promising to crack down on any resistance to his rule. "We will deal with it and crush it," he declared.</P>
<P>According to the <I>New York Times</I>, the new government was so paranoid about the bogus shift in power that security forces immediately tried to locked down sections of Baghdad--with several hotels refusing to let guests go in or out, as American fighter jets patrolled overhead.</P>
<P>A new figurehead can't hide the fact that the U.S. still runs the show in Iraq--and that there's no democracy for ordinary Iraqis. Iraq is the home to the largest permanent U.S. embassy in the world. More than 150,000 U.S. and foreign troops still occupy the country, and U.S. contractors and companies have nearly free reign over Iraq's oil.</P>
<P>"Of course I feel I'm still occupied," artist Qassim al-Sabti told the Associated Press. "You can't find anywhere in the world people who would accept occupation. America these days is like death. Nobody can escape from it." </P>
<P>While the Bush gang can change the thugs in charge of Iraq, they can't hide the truth from growing numbers of people--at home and abroad--who see the occupation for what it is: a brutal grab for oil and empire. That's why Bush's recent European visit to convince NATO leaders to help out in Iraq included the largest security operation ever seen in Ireland--to protect him from antiwar protesters.</P>
<P>At home, a CNN-<I>USA Today</I>-Gallup poll released on the day of the handover found that--by a 2-1 margin--Americans think the turnover of political control to Iraqis is not a sign of success, but a sign of failure because of the country's instability. Another 63 percent said they think U.S. troops will be in Iraq for another three years or more. </P>
<P>Incredibly, George Bush's opponent in the November election--John Kerry--won't promise any better. He claims to have a plan for getting the troops home--by 2008! We can't rely on either party in Washington to deliver democracy to Iraqis. Iraqis deserve the right to determine their own future.</P>


