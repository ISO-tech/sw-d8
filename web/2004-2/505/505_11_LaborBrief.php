<p><a name="LegalAid"></a><font face="Times New Roman, Times, serif" size="4"><b>Legal Aid Society</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Rebecca Kurti and Lucy Herschel, 1199/SEIU</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--About 100 workers protested June 24 over the layoffs due to budget gaps of more than 160 attorneys and 50 support staff at the Legal Aid Society. Legal Aid is New York City's largest provider of free legal services to poor and working people.</P>
<P>The picket was called by 1199/Service Employees International Union (SEIU), which represents the non-attorney staff at Legal Aid, with support from Association of Legal Aid Attorneys (ALAA), United Auto Workers (UAW) Local 2325. Protesters chanted, "Serve the needy, not the greedy" on the street in front of Legal Aid's main office.</P>
<P>Both unions fought by lobbying and rallying on the steps of City Hall, convincing city council members to provide funding specifically to avoid layoffs. In spite of getting the requested funds, management and the Board of Directors at first did not rescind a single layoff notice. </P>
<P>"Management looks at us like we are nothing," said paralegal Magnus Mukoro. "They have no respect for us or the work we do.  If we need to walk out, we need to walk out." The day after the picket, Legal Aid management and the Board of Directors rescinded the layoff notices for most ALAA attorneys. This shows that membership mobilization and political pressure can win.</P>
<P> Last fall, 1199's members voted down a contract, held a major walk-in on management and won a better contract. Both unions at Legal Aid need to work together to pressure management and the Board of Directors through job actions until every last job is restored.</P>


