<P><font face="Times New Roman, Times, serif" size="4"><b>Freightliner</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul Dean</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">PORTLAND, Ore.--About 1,100 workers have voted overwhelmingly to strike a Freightliners plant here, despite the union bureaucrats' recommendation to accept a contract. The current contract ran out June 30. </P>
<P>The International Association of Machinists Local Lodge 1105 voted 713 to 35 to strike, and the Teamsters Local 223 by 117 to 24. Service Employees International Union Local 49, which represents office staff, also voted against the offer. </P>
<P>Freightliner, which makes heavy-duty trucks, is a subsidiary of DaimlerChrysler AG whose truck/bus division reported a quarterly profit of $329 million, up $51 million from last year. Workers made big concessions, including giving back $2 an hour in the 2001 contract and accepting a large reduction in the number of jobs.</P>
<P>But now the company has seen a huge increase in profits and doesn't want to share it. The company expects sales to top $10 billion this year. "They made hundreds of millions of dollars, and they don't even want to give us what we gave back," Teamster Dave Fishel said.</P>
<P>As the union goes back to negotiate again, workers have to be wary of being sold short. Portland has had one of the highest unemployment rates in the country for a long time. It's time for the bosses to pay up, and stop making the workers pay for the their problems.</P>

