<p><a name="UFCW"></a><font face="Times New Roman, Times, serif" size="4"><b>Puget Sound grocery workers</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Darrin Hoop, UFCW Local 1105</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEATTLE--The contract covering 25,000 grocery workers in the Puget Sound region has been extended for a third time, to July 9. Five Locals of the United Food and Commercial Workers (UFCW) and Safeway, Kroger and Albertsons have been locked in negotiations since before the contract originally expired on May 2.</P>
<P>Both sides have agreed to bring in a federal mediator to oversee the talks, which are set to resume July 6. The companies, who are grouped together as the Allied employers, are still demanding around $500 million in concessions. They want massive cuts in health care benefits for current workers and a two-tier contract that would significantly reduce new employees' wages, health care and pension benefits.</P>
<P>The companies, despite losing hundreds of millions in potential profit because of the Southern California strike earlier this year, are still making tons of money. For example, in the first quarter of this year, Kroger's profits were down 25 percent compared to the first quarter last year, but it still made $262 million in profit. </P>
<P>UFCW leaders themselves proposed $120 million in health care concessions--but the companies rejected this offer because it didn't cut enough! Yet the union still hasn't called for a strike vote or mobilized for any mass rallies that could tap into the widespread support within the community.</P>
<P>Union members must demand at least three days to look over any proposed contract before voting on it. A "vote no" campaign will be the only way to let the union know that concessions won't be acceptable.</P>

