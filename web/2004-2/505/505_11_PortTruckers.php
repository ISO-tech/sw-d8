<P><font face="Times New Roman, Times, serif" size="5"><b>Port truckers fighting for pay and union rights</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 2, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">OAKLAND, Calif.--With no relief from high diesel prices in sight, port truckers on the East Coast called on truckers nationwide to turn up the heat on trucking firms and cargo companies this July. According to <I>The Port Trucker</I>, an online truckers' newsletter, "Truckers in the East and Gulf states are passing out leaflets that claim the month of July will be one the trucking companies, ports, and steamship lines will not soon forget."</P>
<P>A wildcat strike launched by port truckers this spring cut the flow of cargo at the Port of Oakland to a trickle--just 178 truck trips a day, out of a usual 1,100. Port truckers, mostly African Americans and Latinos who sometimes call themselves "troqueros," occupy a powerful position in the shipping industry--between ports of entry and the railway lines that carry goods all over the country.</P>
<P>Nevertheless, troqueros' hourly wages are only around $8 an hour. On the West Coast, drivers are paid $50 to $200 per cargo container and, in turn, must pay for fuel, insurance, registration and maintenance out of their own pockets.</P>
<P>With diesel prices over $2.30 per gallon in California--50 cents more than the national average--drivers are being pushed closer and closer to the poverty line. During the April actions in Oakland, troqueros circulated flyers demanding a 30 percent rise in freight rates paid, fuel surcharge increases of 5 percent and additional reimbursement when diesel fuel tops $1.95 a gallon.</P>
<P>The trucking firms have responded with tiny raises and not much else. Drivers are also demanding recognition as workers. In the past, port drivers were mostly employees of trucking firms, with decent wages, pensions, and health care.</P>
<P>Today, they're defined as independent contractors and prevented from organizing unions by anti-trust laws. So far the drivers' actions have been organized by loose networks of workers, their families and friends, announcements on Spanish-language radio stations and informal meetings in parking lots and parks from California to South Carolina.</P>


