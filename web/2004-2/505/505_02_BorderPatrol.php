<P><font face="Times New Roman, Times, serif" size="4">Anti-immigrant sweeps in California round up thousands</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Border Patrol's racial profiling</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sarah Knopp</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 2, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE U.S. Border Patrol has launched a major offensive of anti-immigrant sweeps in Southern California. "They're going to Laundromats and schools and shops, stopping people when they get off the bus," Rev. Arnoldo Abelardo of La Placita Church in Los Angeles told reporters.</P>
<P>The sweeps focused on northern San Diego County and Los Angeles' Inland Empire, but roadblocks and raids have taken place from the Mission district in San Francisco to South Central Los Angeles. U.S. Immigration and Customs Enforcement (ICE)--formerly known as the INS--confirmed that there have been hundreds of arrests. But the exact number is disputed. </P>
<P>Juan Jose Gutierrez, leader of Latino Movement USA and One Stop Immigration Services, told <I>Socialist Worker </I>that if recent detentions at the border are included, as many as 7,000 to 9,000 people were rounded up in June. "They came into the shops and factories and the garment industry [in Los Angeles] and took people from work," Moises Ruiz of Latino Movement USA told <I>Socialist Worker. </P>
</I><P>In the wake of these raids, many undocumented workers have been afraid to go to work, school, church or anyplace else public. Anti-immigrant radio "shock jocks" in the Los Angeles area are trying to contribute to the terror, giving their listeners the phone number for a pro-immigrant rights community group and encouraging listeners to call up and harass them.</P>
<P>This new offensive comes as the Border Patrol received a significant increase in funding since becoming part of the Department of Homeland Security. The new ICE Web site boasts that the agency has developed "a comprehensive interior enforcement strategy...that creates a seamless web of enforcement extending from the border, and beyond, to the worksite." </P>
<P>And if a law pending in Congress--the Clear Law Enforcement for Alien Removal Act--passes, state and local police would be able cooperate with federal immigration officials in deportations.</P>
<P>But many undocumented workers and community groups are standing up against the anti-immigrant tide. On June 13, protesters marched from the cities of Ontario to Pomona, calling the ICE sweeps a form of "racial profiling." Teachers, church leaders and community leaders spoke out at press conferences at ICE buildings in San Francisco, Los Angeles and the Inland Empire.</P>
<P>And on June 26, community organizations came together for an emergency response march and rally through Los Angeles. Some 400 people turned out for the march--which was sponsored by Latino Movement USA, Hermandad Mexicana Nacional, the National Lawyers Guild, the Arab-American Anti-Discrimination Committee and many community support networks, as well as antiwar groups.</P>
<P>The turnout was smaller than previous immigrant rights demonstrations because of the fear created by the sweeps. But thousands of undocumented workers in Los Angeles' garment district lined the streets to show their support. "The politicians know that they need all the hard work that we do," Juan Jose Gutierrez told the crowd. "They can't scare us into keeping quiet." </P>
<P>The groups that planned the June 26 march are calling a statewide day of action on October 16 to protest the 10-year anniversary of the anti-immigrant Proposition 187. We need to build this fight to beat back the right-wing attack.</P>


