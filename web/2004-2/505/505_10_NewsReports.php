<p><a name="Iraq"></a><font face="Times New Roman, Times, serif" size="4"><b>End the occupation of Iraq</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">BURLINGTON, Vt.--About 300 people from all over the state attended a demonstration against the U.S. occupation of Iraq during the last weekend in June. Speakers from groups such as U.S. Labor Against War (USLAW), the American Friends Service Committee, Vermonters for a Just Peace in Palestine and the International Socialist Organization took to the microphone to call out the liars in the Bush administration and to call for U.S. to be brought home.</P>
<P>"Ordinary Iraqis are not our enemy, and we must recognize that a war against the Iraqi people is also a war against us here in the U.S.," said a speaker from USLAW. The deep-seated anger at Bush, however, was deflected into a pro-Democrat Anybody But Bush message from the front, led by Ben Cohen of Ben and Jerry's ice cream fame. A speaker from Military Families Speak Out, who spoke about her struggle while holding up a picture of her only son who is currently serving in Iraq, received the loudest applause.</P>
<P>While many seemed to favor the United Nations assuming control in Iraq, there was also a small but firm anti-occupation sentiment. "Occupation is occupation no matter what color the fatigues are," said one demonstrator, who carried a placard listing the lies told by the Bush administration to justify the war. </P>

