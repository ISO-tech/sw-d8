<p><a name="Cincy"></a><font face="Times New Roman, Times, serif" size="4"><b>Stop the war on the poor</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Jon Daniels</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">CINCINNATI--Community activists organized a June 12 march against the city's all-out war on the poor. The Drop-Inn Center, a homeless shelter, is located in Over-the-Rhine across the street from Music Hall, where the city's elite arrive in limos for expensive concerts. </P>
<P>Since having a homeless shelter that serves a mostly African American clientele conflicts with plans to turn the area into a cultural center, they're trying to force us out. In protest, various community groups organized the march to keep the Washington Park area from gentrification. </P>
<P>After several inspiring speeches, demonstrators marched past the Drop-Inn Center and the District 1 police station to Laurel Park, chanting and speaking out against this city's war on poor people. This is only the latest chapter in the city's drive to punish the poor.</P>
<P>The first battle was to gentrify Main Street in the Over-the-Rhine neighborhood--in the name of "neighborhood improvement." Now two large housing projects are being torn down to make way for townhouses and apartments that former residents can't afford to rent.</P>
<P>To help the developers, the city council passed a ordinance that "forbids the City of Cincinnati from spending, approving or in any way condoning more subsidized low-income development in those areas deem[ed] 'impacted.'"</P>


