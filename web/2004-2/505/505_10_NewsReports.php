<P><font face="Times New Roman, Times, serif" size="4"><b>Gay marriage is a civil right</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By August Beck and Nell Crawford</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">FROM SAN Francisco to New York City, gay marriage activists made their demand loud and clear in Gay Pride parades this weekend: Equal marriage now!</P>
<P>In San Francisco, mock Supreme Court justices marched in front of almost 800 people in the Marriage Equality California (MECA) contingent at Gay Pride. Some 200 couples marched, many with their marriage certificates framed and stuck on wooden picket signs. Many churches in support of gay marriage also attended.</P>
<P>Mayor Gavin Newsom's decision to defy state law and issue marriage licenses earlier this year won him big points in this crowd, with many people wearing "Newsom" T-shirts and chanting "Gavin! Gavin!" "LGBT for Kerry" stickers were always in sight, despite the fact that the Democrat <I>opposes </I>gay marriage and has said that he's in favor of a ban in his home state of Massachusetts.</P>
<P>Jack Zelis, who made a "If not now, when?" sign for the parade, said he was trying counter the sentiment that it was a bad time to demand gay marriage "because it was an election year."</P>
<P>-- Amid the usual floats carrying politicians, drag queens and advertisements for bars, about 75 people marched with Equal Marriage Now! (EMN) in Chicago's annual Gay Pride Parade. The vibrant contingent garnered loud cheers from people lining the parade route, as they chanted, "No more back of the bus!" and "Gay, straight, Black, white--Equal marriage is a right!" and passed out thousands of leaflets urging others to join the fight.</P>
<P>Although Pride is a celebration of gay lives and culture, it commemorates the 1969 Stonewall riot, in which gays and lesbians resisted police repression, sparking the modern movement for gay rights. The need for a new political fight against bigotry was evident for reasons beyond gay marriage.</P>
<P>At three points throughout the march, antigay bigots held homophobic signs and yelled slurs. EMN activists confronted these bigots at each point.</P>
<P>John Kerry supporters led the march, and Barack Obama, a Democrat running for U.S. Senate, sent supporters, although he too opposes same-sex marriage. EMN activists are planning actions to confront these politicians and show opposition to the Federal Marriage Amendment, which will be brought before Congress this coming month.</P>
<P>-- The Marriage Equality New York contingent at the New York City gay pride parade was among several contingents that had gay marriage as their theme. Signs included "Civil unions aren't enough, no second-class citizenship" in English, Spanish and Chinese.</P>
<P>-- In Seattle, one of the most popular contingents in this year's 100,000-strong Gay/Lesbian/Bi/Transgender Pride Day was the gay marriage contingent. People watching from the sidelines roared approval and cheered on seeing the contingent arrive. Supporters of Ralph Nader, who supports gay marriage, were also there to gather signatures to get him on the ballot.</P>
<P>-- In Cincinnati, Ohio, gay rights activists are fighting the bigoted Article 12 law, which specifically prohibits gays and lesbians from seeking protection against discrimination. "Gay rights under attack--what do we do? Stand up, fight back!" was the message of the gay marriage contingent at the pride parade on June 13.</P>
<I><P>Mitch Day, Steve Leigh and Matthew Pillischer contributed to this report.</I> </P>

