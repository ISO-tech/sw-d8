<P><font face="Times New Roman, Times, serif" size="5"><b>Setback for Bush's war on our civil liberties</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 2, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE BUSH administration's attempt to strip foreign prisoners of all rights suffered a major blow from the U.S. Supreme Court. A 6-3 vote by the court gave prisoners at the U.S. naval base in Guant&aacute;namo Bay, Cuba, the right to take their case to U.S. courts. This will allow the 595 prisoners--most held since the U.S. invasion of Afghanistan without being charged--to make appeals in the courts.</P>
<P>The ruling is the latest blow against the abusive U.S. policy in Guant&aacute;namo. In mid-June, the White House was forced to disown a legal memo, which claimed that as president, George W. Bush had the authority to dispense with the rules of the Geneva Convention governing treatment of prisoners.</P>
<P>Earlier investigations of the torture scandal at Abu Ghraib prison in Iraq found that the methods used on Iraqis had first been used in Guant&aacute;namo. Now that the Supreme Court ruling has cleared the way for prisoners to bring lawsuits in U.S. federal courts, details of further abuse are sure to emerge.</P>
<P>The Bush administration also lost in two other cases involving U.S. citizens accused of being "enemy combatants." In the case of Yaser Esam Hamdi, a Saudi-born U.S. citizen captured in Afghanistan in 2001, the court ruled 8-1 that Hamdi had the right to his day in court. "A state of war is not a blank check for the president when it comes to the rights of the nation's citizens," wrote Justice Sandra Day O'Connor.</P>
<P>Jos&eacute; Padilla, a U.S. citizen arrested at O'Hare Airport on charges of conspiring to detonate a radioactive "dirty bomb," also challenged the government's attempt to hold him as an enemy combatant. Even before the Supreme Court ruling, the Padilla case was falling apart. Federal prosecutors admitted that the dirty bomb charge against him was wrong--only to claim that he was planning to blow up apartment buildings instead.</P>
<P>The justices decided to send the Padilla case back to lower courts for technical reasons--but the Hamdi ruling gives his appeal a powerful boost. The American Civil Liberties Union ruling called the Supreme Court rulings "a strong repudiation of the administration's arguments that its actions in the war on terrorism are beyond the rule of law and unreviewable by American courts." With the Bush administration on the defensive, now is the time to step up activism against the government's assault on civil liberties.</P>


