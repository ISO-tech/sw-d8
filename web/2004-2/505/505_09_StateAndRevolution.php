<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Can elections bring socialism?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 2, 2004 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE MOST popular view of the state, even among most radicals, is that its character can change if only the right people were to run it: "Nothing can be more erroneous than the assertion," wrote German Social Democrat Karl Kautsky against revolutionary socialists in 1919, "that parliamentarianism and democracy in their very essentials are bourgeois institutions.</P>
<P>"If in any parliament the bourgeois elements are to be in the majority, then parliamentarianism will be bourgeois in character; and if these parties prove to be of no use their parliamentarianism is also useless. But as soon as a Socialist majority appears in parliament, the whole situation is radically changed."</P>
<P>The Polish-born German revolutionary Rosa Luxemburg took on this view two decades before Kautsky wrote these words. Even if socialists were able to achieve a majority in parliament in a given country, she argued, this would not signal the victory of socialism.</P>
<P>Rather than adapt to the socialists, the bureaucratic state machine and the most powerful of the wealthy elite put tremendous pressure on the socialists to adapt to the system. And in the event that they feel that their interests are threatened, the ruling class rally around the most trusted state institutions--the police, the army, the state bureaucracy and corrupted party politicians--against parliament if necessary:</P>
<P>"In this society," she wrote, "the representative institutions, democratic in form, are in content the instruments of the interests of the ruling class. This manifests itself in a tangible fashion in the fact that as soon as democracy shows the tendency to negate its class character and become transformed into an instrument of the real interests of the population, the democratic forms are sacrificed by the bourgeoisie and by its state representatives."</P>
<P>This is not some theoretical debating point, but has often been the bitter historical experience of the workers' movement internationally. In Chile, for example, Salvador Allende's reformist socialist government was overturned in a bloody military coup in 1973. Allende was committed to the peaceful, "Chilean" road to socialism.</P>
<P>He accordingly told his working-class and peasant followers not to do anything to "provoke" the capitalists by seizing control of factories or farms. Meanwhile, Chile's bosses, with the support of the U.S., looked on warily as workers and peasants demonstrated and struck for change. When Allende invited generals into the government and assured his followers that the military would "respect the constitution," he lulled the working class into a false sense of security.</P>
<P>When it was clear to them that Allende wasn't capable of restraining his own followers, the generals overthrew his government and installed a brutal military dictator, Pinochet. Allende had disarmed the working class enough for them to be defeated, but not enough for them to appease the ruling class.</P>
<P>Of course, it's true that the ruling class sees no need for a coup if the working class restrains its own actions to the point where capitalist property is not threatened, i.e. if they peacefully accept their lot. But if workers really try to organize a society based on collective ownership and control of production and wealth, the bosses will fight tooth and nail to keep what they have.</P>
<P>The state exists, in the last analysis, as an instrument of rule by the minority, exploiting class. Often, they can rule effectively without the direct use of force, by some combination of force and consent. But when consent breaks down, they are willing to lop off the representative portion of the state and opt for direct force.</P>
<P>The state may therefore take different forms, but its essence is, to quote Marx and Engels, "bodies of armed men, prisons, etc.," that act as "the Executive committee for the management of the common affairs of the ruling class." Elections are an important means of gauging mass consciousness and getting out socialist ideas to a broader audience. But we should never confuse parliament or congress with real state power.</P>


