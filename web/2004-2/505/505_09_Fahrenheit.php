<P><font face="Times New Roman, Times, serif" size="4">Moore's <I>Fahrenheit 9/11</I>packs theaters</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A movie for our side</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Adam Turl</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 2, 2004 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2"><B><i>Fahrenheit 9/11</i></B>, a documentary by Michael Moore.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE HANDOVER of "sovereignty" to CIA asset Iyad Allawi is another farce in a long line of farces and lies used to give cover to the U.S. invasion and occupation of Iraq. Judging by the response to Michael Moore's <I>Fahrenheit 9/11</I>--millions of people are sick of the shinola and looking for answers.</P>
<P>More than half of Americans, in poll after poll, have said that the war on Iraq wasn't worth it and the U.S. should withdraw. So it's no wonder that <I>Fahrenheit 9/11 </I>is packing them in from New York City to Oklahoma City, debuting at number one at the box office--despite showing on a third of the screens reserved for summer blockbusters.</P>
<P>Moore delivers what folks expect from him as far as making fun of the powerful. While teeing off on one of his countless golf outings since becoming president, George W. Bush stops to emphasize that we must "stop the terrorist killers" and, without missing a beat, orders the assembled media, "Now, watch this drive."</P>
<P>But the film delivers more than skewering Bush--it indicts the war and occupation by putting its victims up front. Moore's voice is played down more so than in his other films. Instead, the film focuses on the voices of soldiers, their families and Iraqis themselves.</P>
<P>An Iraqi mother asks why her son has been taken away in the dead of night on Christmas Eve. Lila Lipscomb, the mother of a soldier from Flint, Mich., describes how she had hated antiwar protesters but then turned against the war when her son is killed. She says she recommended her children join the military because it was a "good job opportunity."</P>
<P>Moore shows the bloodied faces of Iraqis whose bodies were destroyed by shock and awe. A man clearing rubble finds a part of his neighbor's corpse, a young woman not even 20 years old. Moore shows the racism of the war, as soldiers desecrate an Iraqi man's body, calling him, "Ali Babba."</P>
<P>Right wingers, unsurprisingly, are up in arms about this movie. The conservative group, Citizens United, filed a complaint that the movie's ads violated campaign finance laws.</P>
<P>And then there's the usual hue and cry that Moore isn't being objective. It's sad, and comic, to see Fox News' hacks bemoan the lack of Moore's objectivity. People are lining up to see this movie because there's a hunger for the truth--instead of the "fair and balanced" lies that outfits like Fox feeds us.</P>
<P>In Moore's film, a corporal says he will refuse--if he is ordered--to go back to Iraq and "kill other poor people." And the audience cheers. There is no way to be "objective" about that--and Michael Moore is right not to pretend to be.</P>
<P>The horrors of war and the corporate interests behind them expose a sick society--summed up aptly by one businessman in <I>Fahrenheit 9/11</I>: "War is bad for people but good for business."</P>
<P>Of course, there are some problems with the movie. Moore skewers the USA PATRIOT Act--but not the roundup of thousands of Arabs and Muslims. His expos&eacute;s of Bush's connections to Saudi money might under certain circumstances be used to justify anti-Arab racism.</P>
<P>And the most important thing left unanswered is what to do when you leave the theater, seething with righteous anger. Moore has made it plain that his goal is to show Bush the door in November.</P>
<P>MoveOn.org and others are hitting the streets outside theaters to register voters and stump for Democrat John Kerry. Put aside for a moment, that Kerry voted for the war and supports the occupation--making him as complicit as Bush.</P>
<I><P>Fahrenheit 9/11</I>--while it focuses on Bush--makes it clear the Democrats don't offer us much. For example, it includes footage of Al Gore ramming through the Supreme Court decision to install Bush. One by one, Congressional Black Caucus members condemn the Black voter disenfranchisement as Gore shuts them down. Not one Democratic senator would sign on so they could be heard.</P>
<P>It's not surprising that Moore isn't sure what else to do. There's a big job ahead for those who opposed the war--which ultimately was never about just Bush, or this or that policy.</P>
<P>Moore--consciously or not--shows who can begin to take that on. It's not John Kerry. It's the soldiers who are questioning the war, the working-class people being driven into poverty and the Iraqi resistance who have the power to end the occupation--and much more.</P>


