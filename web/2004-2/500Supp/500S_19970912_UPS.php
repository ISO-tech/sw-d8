<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">UPS strike victory offers fighting example for new workers' struggles</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The return of class politics</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 12, 1997 | Page 3</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>THE STRIKE victory by the International Brotherhood of Teamsters over United Parcel Service (UPS) faded from the headlines with the passing of Labor Day.</P>
<P>But there can be no mistaking the strike's lasting impact on U.S. society and politics. By winning the most important strike in 25 years, UPS workers set a fighting example for other unions.</P>
<P>Just days after the UPS settlement, workers at the Bay Area Rapid Transit system voted to strike by a 90 percent margin. On September 7, they shut down San Francisco subways. Two days earlier, Madison, Wis., teachers organized an overwhelmingly successful--and illegal--sick-out in their fight for a fair contract. Teachers in small school districts in downstate Illinois walked picket lines. Even in the conservative atmosphere of Philadelphia's Catholic schools, lay teachers struck for decent pay and union rights.</P>
<P>No wonder the bosses' magazine <I>Business Week </I>called the UPS strike "a wake-up call for business...[T]he strike touched a nerve with a broad swath of the public feeling a squeeze that's borne out by statistics. According to the Commerce Department, wages and other compensation as a percentage of national income has declined for four consecutive years, from 1993 to 1996, while corporate profits as a share of national income rose sharply over the same period. Why, many people found themselves asking, are so many being left behind when companies are doing so well?" </P>
<P>In other words, the UPS strike brought to the surface that great secret of U.S. politics: class. Despite the media hype about a new economic "golden age," U.S. workers are no different than their brothers and sisters in South Korea and France, who in recent months have launched mass strikes to defeat attacks on their wages and working conditions. </P>
<P>The UPS strike has given hope and inspiration to everyone fed up with the direction of U.S. society--not just rampant corporate greed, but savage cuts in welfare and social spending, police brutality, immigrant-bashing and oppression of every kind. Even the <I>New York Times </I>had to take note of the "buoyant mood" of an August 29 demonstration in New York, the second mass protest in as many weeks against the police torture of Haitian immigrant Abner Louima.</P>
<P>Most important, the UPS strike showed that a revived workers' movement can be the basis of a political alternative to the Republican and Democratic hacks who do the bidding of their big-business paymasters. To be successful, a fighting labor movement requires the solidarity of all workers and political independence from a government that serves the employers' interests.</P>
<P>These principles are at the core of socialist politics. Every great step forward for the U.S. labor movement--in the 1880s, the early 1900s and again in the 1930s--coincided with the growth of mass socialist organizations. Building on the success of the UPS strike means rebuilding a genuine socialist alternative.</P>
<P>The Teamsters' victory over UPS cannot guarantee future success. The employers will dig in to defend everything they've stolen from workers. But the strike victory shows the possibility for workers to make their greatest gains in a generation--and the potential of building a socialist current in the working class.</P>

