<P><font face="Times New Roman, Times, serif" size="4">A crucial victory for abortion rights activists</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Operation Failure in Buffalo</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mark Larson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 1992 | Page 16</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>BUFFALO--"Operation Rescue, wherever you go, you'll remember Buffalo!" was what hundreds of pro-choice clinic defenders chanted at a demoralized Operation "Rescue" (OR), as pro-choicers chased OR out of town.</P>
<P> OR had promised four weeks of blockades of Buffalo clinics on the scale of their August 1991 attack on Wichita, Kan. Unlike Wichita, in Buffalo, OR was met from day one by angry pro-choice crowds chanting, "You're not in Kansas anymore, we'll defend the clinic door!" From the time OR started its attacks on clinics April 20, it was clear that the clinic defenders controlled access to the clinics and that Buffalo would be a victory for abortion rights.</P>
<P>Wichita focused national attention on OR's anti-woman message, and gave anti-abortion bigots a boost of confidence. It was a clear defeat for abortion rights activists, and since then, pro-choice activists have rallied around the cry "No More Wichitas!"</P>
<P>As soon as OR announced its Buffalo plans, abortion rights groups from all over the country began to make plans to defend the clinics. This organizing went against the "accepted wisdom" of the mainstream pro-choice movement which sought instead to intensify the losing tactics used in Wichita: legislation, court injunctions against OR and relying on the police to keep the clinics open.</P>
<P>Groups like the National Organization of Women (NOW), despite the influence they have over thousands in the pro-choice movement, did not mobilize nationally for Buffalo, and even discouraged clinic defense. The Cincinnati NOW chapter, for example, wanted to send busloads of people to Buffalo, but canceled plans because of National NOW policy.</P>
<P>Despite these obstacles, there were thousands of activists who were sick of remaining passive and defensive in the face of attacks on abortion rights in the streets and in the legislatures.</P>
<P>By April 20, OR leaders were publicly revising and scaling back their expectations. OR no longer felt confident about its ability to close abortion clinics.</P>
<P>On April 20 and 21, everyone expected the "big hit." But, outnumbered by the pro-choice presence, OR could only muster small numbers of supporters for prayers across the street from the clinics. The daily, mass clinic defenses clearly showed who had the edge in this battle, and it showed how easily OR can be defeated when challenged.</P>
<P>On April 22, OR worked out a media stunt with the cops. One hundred and ninety OR activists knelt in the street, just in front of the clinic defense lines, and were carted off by cops without challenging access to the clinic. The media was hungry for OR's big day, and pounced on the event. They reported it as if it were a blockade and took OR leaders' word that "many women had been scared away" by the event.</P>
<P>But the clinic defenders were well aware of the situation and were made more confident. By week's end, Patricia Ireland, the new president of NOW, made an appearance in Buffalo. In light of OR's defeat in Buffalo, Ireland was forced to express support for those defending the clinics. "We support clinic defense," she told a CNN reporter. "We have seen that this is a strategy that works."</P>
<P>Ireland's comments indicate that the abortion rights victory in Buffalo can renew the confidence of pro-choice activists nationwide. But NOW--and other mainstream groups--have yet to show a resolve in building militant action to shift their attention away from electoral politics.</P>
<P>The Buffalo victory shows that the energy and anger of thousands can win battles. Those in Buffalo were not politicians: they were students, working women and men, and others directly threatened by OR's activity. Our victory proved that people who are most affected by restrictions on abortion are the ones capable of offering real political leadership.</P>

