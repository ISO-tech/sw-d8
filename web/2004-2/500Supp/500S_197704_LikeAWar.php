<P><font face="Times New Roman, Times, serif" size="5"><b>"It's like a war here"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 1977 | Page 16</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>STEARNS, Ky.--"It's like a war here," according to Joe Perry, the McCreary County sheriff. He was describing the picket lines at the Stearns Justus mine. </P>
<P>In early March, the 200 strikers were told by Frank C. Thomas, the president of Stearns Mining Co., that unless they returned to work, replacements would be hired. Then, the following Saturday, the Stearns security guards were escorted through the picket lines by state troopers in riot gear. </P>
<P>It was after this that the real shooting started.</P>
<P>Luther Spradlin lives on the hilltop just above the mine fan, the place where the guards are concentrated. He says that's the major source of the shooting. "I came out of my door the other day and stepped into the yard," he said. "That's when I heard the first shot. It came down from the fan. There must have been 50 or 60 shots in all, coming as fast as I could count them. You could hear them plinking through the trees."</P>
<P>The strikers on the picket lines have put up sand bags. There are sand bags on the picket side of the road, according to Sheriff Perry, "because there's been so much shooting." He says he's counted 21 bullet holes in the union hall.</P>
<P>One of the strikers said he was one of the targets. "I was sitting right there on the picket line yesterday around noon, when suddenly they cut loose," he said. "It was just like a turkey shoot for 15 to 20 minutes. If you put your head up above the sand bags, you were fair game."</P>
<P>The guards have sandbags, too. Perry says he has counted 50 bullet holes in their building. Now, he says, "Anything could happen. If other men try to come in to work in this mine, there's going to be a confrontation. I don't think these men are going to let somebody come in and take their jobs."</P>
<P>The strike at the Stearns Mining Company Justus Mine is now eight months old. It began as a struggle for union recognition. The miners voted 126 to 57 to be represented by United Mine Workers. In July, they walked off the job.</P>
<P>The Stearns Company is a subsidiary of the Blue Diamond Coal Company, a Tennessee company that operates a number of mines in Eastern Kentucky. It is the same company that owns the Scotia mine. Scotia mine is also nonunion. It was the scene of last year's disaster, an explosion in which 26 men were killed.</P>
<P>The UMWA bargained with Blue Diamond until January 28, but the company consistently refused to accept the union's demand on safety, wages and other fringe benefits.</P>
<P>The key issue is safety. The Stearns miners voted to join the UMWA just two weeks after the explosions at Scotia.</P>
<P>"Stearns Mining has been scabbing here since the UMWA was beat back in 1953," said one miner. "Blue Diamond took it over more recently, but on unions and safety, they're just as bad. The way I look at it, they killed 26 men over there at Scotia and got away withy it. So they figure it's no big deal if they get another bunch over here."</P>
<P>On Monday, March 14, the wives and widows of the Stearns miners picketed the McCreary County Courthouse in Whitley. They said the real trouble began on Saturday, March 12, when heavily armed state police were used to escort strikebreaking security guards across the union lines.</P>
<P>They reported that three carloads of troopers, all wearing flak jackets and full riot gear, crossed the lines with a group of seven hired guards. They also arrested and jailed two of the strikers. The troopers' intervention came just at the exact time that Frank Thomas, the Stearns president, was ordering the strikers back to work.</P>
<P>"I think these men have a right to picket and a right to a union contract that guarantees them safety," said one of the women, the widow of a McCreary miner, and the mother of two Stearns miners.</P>
<P>"These security guards are trying to mow our men down like dogs," said Loretta Ball, the wife of another Stearns striker. "They've made a sifter out of the miners' building out there on the picker line with all their shooting. Now the state police have been sent in to help them. That's why I'm here with my sign."</P>

