<P><font face="Times New Roman, Times, serif" size="4">Mother of slain soldier arrested for speaking out</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>They sent her son to die for oil</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 1, 2004 | Page 1</font></P>

<P><font face="Times New Roman, Times, serif" size="3">SUE NIEDERER listened quietly as Laura Bush addressed a rally of her husband's supporters in Hamilton, N.J., in mid-September. But when the First Lady started talking about the war on Iraq, Niederer couldn't stay silent.</P>
<P>"When are yours going to serve? When are you going to fight?" yelled Niederer. She has every right to ask. Her 24-year-old son, Seth Dvorin, was killed in action in Iraq on February 3. Of course, the 22-year-old Bush twins, Jenna and Barbara, won't ever come close to military service--they'll be far too busy partying and making a fortune off the Bush family's connections.</P>
<P>Sue Niederer knew that her message wouldn't go over well with the Bush faithful. But she did have a ticket to the event, and she thought that perhaps her cry of grief would at least be respectfully heard. "I thought they would send someone up to me and bring me back and let me tell her in private about how I felt, and many others, too," Niederer said. "Not these people."</P>
<P>Instead, she was surrounded by Secret Service agents, drowned out by chants of "Four more years!" and quickly hustled outside--to be arrested on charges of "defiant trespass." The charges were dropped the next day, but the experience demonstrates just how far the Bush administration will go to keep the real facts about what's happening in Iraq from making it into the public spotlight.</P>
<P>Last week, Iraqi Prime Minister Iyad Allawi toured the U.S., trumpeting "progress" in war-torn Iraq. And George Bush had the gall to defend his aircraft carrier stunt 17 months old when he declared "Mission accomplished."</P>
<P>But U.S. soldiers on the ground in Iraq tell a different story. "We shouldn't be here," one Marine infantryman who has been on the front lines in Ramadi told a reporter. "There was no reason for invading this country in the first place. We just came here and [angered people] and killed a lot of innocent people. I don't enjoy killing women and children. It's not my thing."</P>
<P>Meanwhile, the Pentagon brass proved their barbarism again last week with deadly missile strikes in Falluja and the Baghdad neighborhood of Sadr City--two flash points of the resistance to occupation. The military claimed that its attacks were surgical strikes against leaders of the Iraqi insurgents--but a hospital spokesperson in Baghdad said dozens of people, including women and children, were treated for injuries from the missile attack.</P>
<P>No wonder the resistance in Iraq has grown even in parts of the country previously considered free of "insurgents," according to Kroll Security International, a private security firm working for the U.S. in Iraq. The resistance takes many forms, according to Kroll--from complex ambushes involving 30 guerrillas to children tossing Molotov cocktails at army patrols.</P>
<P>Gen. John Abizaid, the top commander in Iraq, admitted that the U.S. will have to launch a savage new offensive against Iraqi resistance fighters before elections scheduled for January. But until the November election in the U.S., the Bush administration wants to avoid an avalanche of casualties that will accompany a direct confrontation.</P>
<P>So for now, it's retaliating against attacks with 500-pound bombs. "Every time you drop a 500-pound bomb, it's got something like a 500-meter kill radius--everything within 500 meters is going to be wiped out," Mike Hoffman, a founder of Iraq Veterans Against the War, told <I>Socialist Worker.</I> </P>
<P>"So you might get one or two 'insurgents' or 'terrorists' or whatever the administration wants to call them, but you're going to kill civilians. You're going to kill women and children and grandparents. When you kill those people, for every single innocent you kill, you're adding three or four people to the insurgency."</P>
<P>For Hoffman, the only solution--for ordinary Iraqis and U.S. soldiers alike--is to bring U.S. troops home now and end the occupation. "Our presence right now just makes it worse," he says. "The longer we stay, the longer and more bloody the conflict is going to be. There's no good choice in Iraq. It's a mess. But our presence is just making things worse. We should not be there."</P>


