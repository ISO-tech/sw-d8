<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Behind the hot air from Bush and Kerry...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Are they really that different?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 1, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WITH THE Democratic and Republican presidential tickets preparing for a series of debates, you'll be hearing more than ever about their "sharp differences." You'll read about their "emotionally charged arguments" and radically opposed "visions of America."</P>
<P>"Through history, there haven't been too many elections that take place in wartime," Barbara Kellerman, research director at the Center for Public Leadership at Harvard University, told reporters. "And the amount of animosity generated on both sides of the campaign is higher than in the past. So one could argue that this one has more importance, and the level of interest has ratcheted up." </P>
<P>But for all the buildup, the debates will be tame affairs, where the candidates restate well-worn lines from their campaign speeches in a highly controlled environment. That's because they planned it that way. </P>
<P>Representatives of the media and the Bush and Kerry campaigns signed a 32-page document that specified everything from the debate format to the height of the podiums on stage. The rules prevent the candidates from questioning each other--or the moderators from asking follow-up questions. </P>
<P>And it goes without saying that the Democrats and Republicans have agreed to keep alternative voices, like independent presidential candidate Ralph Nader, out of the debates. So rather than being a "showdown" between opposing agendas, the debates will be a corporate-sponsored infomercial for each party. </P>
<P>But isn't U.S. democracy supposed to be based on "robust debate"? Yeah right. This year's fake debates are perfectly in keeping with a political system that is designed to restrict the acceptable areas of disagreement to a narrow spectrum.</P>
<P>The Democrats and Republicans exist to define the limits of "legitimate" political debate acceptable to the Washington establishment. So while this year's presidential debates will highlight points of difference between Bush and Kerry, they will also determine what <I>won't</I> be up for discussion in Election 2004. </P>
<P>Opinion polls suggest that a majority of Americans think the war in Iraq was a mistake, and a significant number want the U.S. to end its occupation now. But no one on the debate platform will represent those positions. That's because the U.S. ruling class has decided it has too much "credibility" invested in Iraq to get out. Behind their sound bites and posturing, Bush's and Kerry's real plans for Iraq hardly differ at all. </P>
<P>Opinion polls also show strong support for a government-guaranteed health care system. But the politicians and the press don't even consider this an option worthy of discussion. Chalk up another major issue that won't make it into the debates.</P>
<P>Ralph Nader supports bringing the troops home and health care for all. But the two parties have assured that he won't be invited to the debates--just as the Democrats have worked overtime to deny voters the chance even to vote for a left-wing alternative by trying to knock the Nader-Camejo ticket off ballots in states across the country. </P>
<P>For the tiny minority that rules this country, the two-party system plays an essential role--providing a fa&ccedil;ade of democracy behind which they continue to pull the strings. If one party falls out of favor with voters, there's always the other one--with predictable and non-threatening policies--waiting in the wings. </P>
<P>The truth is that the minimal differences between the parties mask an even greater agreement between them--above all, on the central goals of protecting corporate power and asserting U.S. interests around the globe.</P>
<P>So remember this when the pundits go on--and on and on and on--about the "stark choice" presented in this year's election. Whoever comes out ahead after November 2, America's rulers have already won.</P>


