<P><font face="Times New Roman, Times, serif" size="4">One-day strike puts governor on the defensive</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Kentucky teachers fight for health care</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Molly Seifert</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 1, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">AS YOU drive into Kentucky, you'll be greeted by a cheery road sign that reads: <I>Kentucky, Where Education Pays. </I>Maybe it should read, <I>Where Teachers Pay.</I> Earlier this month, Kentucky Gov. Ernie Fletcher announced plans to change the state's health insurance for teachers, school employees and state workers beginning January 2005.  </P>
<P>Kentucky teachers' salaries rank 40th in the nation, yet they pay the highest health insurance costs. The new plan will force state employees to pay even higher premiums and deductibles, while reducing benefits. </P>
<P>A huge outcry followed the governor's announcement of the new plan as state workers were supposed to begin enrolling in the new health insurance plan September 27. Rank-and-file members of the Kentucky Education Association (KEA) organized a walk-out on that date, forcing 23 school districts to close in protest of Fletcher's plan.</P>
<P>The action has already forced Fletcher to retreat, calling legislators to the capitol for a special session on October 5 to help craft another health insurance plan for 2005. Against the wishes of KEA president Francis Steenburgen, rank-and-file KEA members voted in favor of a state-wide indefinite strike beginning on October 27 if the new health insurance plan doesn't restore current benefits.</P>
<P>Steenburgen urged KEA members not to strike before the November elections and to take a "wait-and-see" approach. Disgusted Kentucky teachers rejected her pleadings, saying they have "waited and seen" long enough. </P>
<P>"I care about my family, I care about teaching, I care about my school, and I care about my fellow teachers," said Troy Styer, a Campbell County High School teacher. "I have both a bachelor's and a master's degree. Last year was my 16th year teaching. In addition to my teaching salary, I had four stipends for extra duties on my paycheck. My kids were still eligible for reduced lunch at their school, reduced lunch...Do you get it? This is the only job I ever wanted to have. I feel so bad [about the increased health costs]--and frankly at this point betrayed."</P>
<P>Kentucky educators and state employees must continue to organize independently of the KEA leadership and demand affordable health insurance <I>now</I>, not later.</P>


