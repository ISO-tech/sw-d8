<P><font face="Times New Roman, Times, serif" size="4">Farm Labor Organizing Committee organizes Mt. Olive Pickle</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>A historic victory in N.C.</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Julie Southerland</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 1, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">RALEIGH, N.C.--More than 8,000 "guest" workers in North Carolina won a union and a contract on September 16, after a long battle and a five-year boycott of the Mt. Olive Pickle Company's products initiated by the Farm Labor Organizing Committee-AFL-CIO (FLOC). This is an unprecedented victory for migrant workers, especially in North Carolina, a right-to-work state. </P>
<P>These will be the first such workers to win union representation and a contract in the history of the United States, and it is the largest union contract in North Carolina's history. It is also an important victory because of the agricultural industry's almost exclusive employment of undocumented workers.</P>
<P>The contract between the North Carolina Growers Association (NCGA) and FLOC includes a non-discrimination clause to protect workers who support the union from being blacklisted. It also requires representation in labor camps to oversee the implementation and protection of labor rights laid out in the union contract. </P>
<P>A sidebar agreement was reached with the NCGA that will raise workers' wages and prices to growers by 10 percent over the next three years. "We will continue struggling and give it all we got, because there is still work to do," said worker Jose Hernandez-Coronado in a FLOC press release.</P>
<P>"We will never forget those that started this, those that made it possible, those workers and leaders who were in the front lines of the campaign and the union. Right now we do it for ourselves and for our families in Mexico, but we also sign this contract for the future generations who will come in the coming years. Hasta la victoria, somos hermanos en la lucha."</P>


