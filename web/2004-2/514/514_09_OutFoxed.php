<P><font face="Times New Roman, Times, serif" size="5"><b>Fox in the media henhouse</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Cindy Beringer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 1, 2004 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2"><I><B>Outfoxed: Rupert Murdoch's War on Journalism, </B></I>a documentary by Robert Greenwald.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WITH A slew of recording devices, lots of coffee and strong stomachs, a team of volunteers across the country watched and recorded Fox News programming 24 hours a day for months. The result is <I>Outfoxed: Rupert Murdoch's War on Journalism, </I>a documentary that proves definitively the lie in Fox's "fair and balanced" claim.</P>
<P>The use of news footage interspersed with interviews with former Fox employees, analysis of media critics and memos from Fox news chief John Moody makes for an entertaining indictment of the network. That Fox is biased comes as no surprise to those who watched the network scoop the president in declaring war immediately after September 11. And its Republican bent is certainly no secret.</P>
<P>The film shows a news program that begins with a daily logo of a page torn from a calendar with a voiceover announcing "only 217 days until George W. Bush is re-elected." News reporting is indistinguishable from commentary: For example, "The market is down on John Kerry" or  "The economy is behaving like it's on steroids at the moment."</P>
<P>With a flag constantly flapping in the background and weird slogans moving briskly across the bottom of the screen, Fox News uses repetition to impress its distorted views upon viewers.</P>
<I><P>Outfoxed </I>takes this technique and turns it against the network, using its own rapid-fire succession of news clips of repeated phrases in its assault on Fox-style "journalism." Repeated often enough, anything can become a fact. And, according to Fox, "some people say" lends enough authority to make something news.</P>
<P>It's worth the price of the ticket to see vicious host Bill O'Reilly get his comeuppance. <I>Outfoxed </I>features O'Reilly's interview with antiwar protester Jeremy Glick. Despite O'Reilly's personal attacks and a volley of "Shut ups," Glick was able to get across his message--that the U.S. had trained al Qaeda, the group that had killed his father in the World Trade Center on September 11.</P>
<P>Glick was eventually thrown off the program and told to leave the studio lest O'Reilly cause him physical harm. Immediately following this scene is footage of O'Reilly on the next day's show distorting Glick's statements. O'Reilly claims Glick had to be removed because he was "out of control"--and O'Reilly is exposed for the lying lunatic he is.</P>
<P>Although <I>Outfoxed </I>was distributed by MoveOn.org in an effort to aid the election of the Anybody But Bush candidate, John Kerry, this documentary tells a story that viewers can take beyond the 2004 elections. Media critics end the film with a call for citizens to organize to change not only the deceptive habits of Fox News, but all the other news sources that copy it.</P>
<P>The success of this documentary and others like it is good news for activists. The public is developing a big appetite for the truth that they realize their government and their media is keeping from them.</P>

