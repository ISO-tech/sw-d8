<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The real scandal of the U.S. media</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 1, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">AS LONG as Dan Rather is making apologies, we've got a suggestion for another. This isn't about the bizarre case of CBS News' use of forged documents that contained accurate information about George W. Bush avoiding National Guard duty during the Vietnam War.</P>
<P>We're waiting for Rather to say that he's sorry about his statement soon after the September 11 attacks: "George Bush is the President. He makes the decisions. He wants me to line up, just tell me where." With that declaration, Rather underscored the narrowness of the differences between supposedly liberal CBS News and the Republican propaganda mill otherwise known as the Fox News Channel.</P>
<P>Now, however, the right-wing attack on the "liberal media" is in full hue and cry over CBS's use of documents that were faked. Never mind that the information cited by Rather--which exposed Bush as a politically connected draft dodger--is real enough, documented long ago by investigative journalists.</P>
<P>If the Bush draft-dodging scandal is new to the corporate media, it's because news organizations refused to challenge anything coming from the White House--most notoriously, the charges that Iraq possessed weapons of mass destruction.</P>
<P>Today, the chaos in Iraq and the pressures of an electoral contest have brought more scrutiny of Bush's lies. Tellingly, however, CBS chose to broadcast the "smoking gun" story about Bush's Guard record rather than a far more important piece on how the U.S. used manufactured documents about Iraq's non-existent effort to obtain yellowcake uranium to make nuclear weapons.</P>
<P>Yet even here, the focus on paper trails and "smoking guns" misses the bigger scandal: the connivance of the corporate media in creating a hyper-patriotic, pro-war atmosphere prior to the invasion of Iraq. Commenting on the postwar journalistic investigations of the failure to find weapons of mass destruction, media critic Michael Massing wrote, "Watching and reading all this, one is tempted to ask, where were you all before the war? Why didn't we learn more about these deceptions and concealments in the months when the administration was pressing its case for regime change--when, in short, it might have made a difference?"</P>
<P>The reason is that the media is embedded--not mainly with frontline troops, but in the corporate boardrooms and the corridors of power in Washington, where the real direction of U.S. politics is decided. It's not a question of conspiracy, but a coincidence of interests.</P>
<P>Mainstream journalists who dissent will find themselves accused of lack of patriotism--and their careers ended. Dan Rather himself admitted as much before a television audience--though in Britain. "It's that fear that keeps journalists from asking the toughest of the tough questions," he said.</P>
<P>The shame of the media--at CBS and elsewhere--isn't the use of made-up memos or bogus sources. The real scandal is in plain sight in the perfectly legal--and thoroughly corrupt--business relationships that dominate U.S. journalism.</P>


