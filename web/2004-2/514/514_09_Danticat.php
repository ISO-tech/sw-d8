<P><font face="Times New Roman, Times, serif" size="5"><b>Stories that demand justice in Haiti</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Sarah Hines</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 1, 2004 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">Edwidge Danticat, <B><I>The Dew Breaker. </B></I>Knopf, 2004, 256 pages, $22.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">TWO HUNDRED years after the victorious slave rebellion on the island of Santo Domingo, the army of its former colonial masters, along with U.S. forces, has once again invaded, overthrew an elected leader and occupied the country. While the U.S. government and the compliant media depicted the right-wing death squads as "freedom fighters," many Haitian Americans spoke out against these outrages.</P>
<P>In this context, Edwidge Danticat's <I>The Dew Breaker, </I>a collection of interconnected short stories, is an important contribution to telling the truth about Haiti's past and present. Danticat artfully interweaves historical and political themes into the lives of her characters, illustrating how the crimes committed against the Haitian people under the Duvalier dictatorship of the 1970s and '80s continue to haunt Haitian immigrants and their progeny in the U.S.</P>
<P>"Dew breakers" was what Haitians called the torturers under Duvalier--"the hundreds who had done their jobs so well that their victims were never able to speak of them again." The Tontons Macoute who terrorized the population were named, Danticat explains, for the "mythic figure of the Tonton Macoute, a bogeyman who abducted naughty children at night and put them in his knapsack."</P>
<P>They were called "dew breakers" because they broke into people's homes "mostly it was at night. But often they'd also come before dawn, as the dew was settling on the leaves, and they'd take you away." </P>
<P>Though Danticat doesn't spell out the U.S.'s role in supporting Duvalier, there are hints. In "Book of Miracles," the Haitian American community organizes to try to uncover Emmanuel Constant, the Duvalier-era war criminal that the U.S. allowed to relocate to New York City after the dictatorship's fall.</P>
<P>In the title story, "The Dew Breaker," a preacher is viciously pursued and murdered by the Duvalier regime for preaching against it. His sermons include such subversive lines as "What will we do with our beast."</P>
<P>A foreign commentator writes, "'These people don't have to go far to find their devils. Their devils aren't imagined; they're real.'" Unfortunately, the devils are still very real.</P>

