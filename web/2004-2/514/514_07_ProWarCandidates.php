<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>No choice between pro-war candidates</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 1, 2004 | Page 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">GOING INTO the first of presidential debates, John Kerry seemed to be recovering some of the ground he lost to George W. Bush after the Republican convention. Two related developments can explain this.</P>
<P>First, the war in Iraq forced itself back into the front pages with the spike in attacks, kidnappings and assassinations. This gave the lie to the Bush fantasy--promoted over and over at the Republican convention--that "freedom was on the march" in Iraq.</P>
<P>And second, Kerry decided to step up his criticisms of Bush's handling of the war. "Invading Iraq has created a crisis of historic proportions, and if we do not change course, there is the prospect of a war with no end in sight," Kerry said in his September 20 speech at New York University.</P>
<P>He said Bush "misled, miscalculated and mismanaged every aspect" of the Iraq war. For the normally sleepwalking Kerry, this was about as close to a firebrand statement as he can get. It was music to the ears of Kerry's supporters who have been urging him to hit Bush where he is most vulnerable--on the Iraq war.</P>
<P>But Kerry supporters are mistaken if they think Kerry would end the war or get out of Iraq if he were elected. Kerry's more aggressive posture against Bush recently is a change in style, not in substance.</P>
<P>Earlier in the presidential campaign, Kerry convinced himself that most Americans were already soured on Bush, and were ready to look to him if only he presented himself as a "commander in chief." Thus, the Democratic convention was long on generals, militarism and praise for Kerry's service in Vietnam--and short on criticisms of Bush.</P>
<P>Bush's campaign and its surrogates, like the Swift Boat Veterans for Truth, spent the next month tearing down Kerry. So Kerry was left with only one card to play--to amplify his criticisms of Bush's conduct of the war.</P>
<P>But "amplifying criticisms" is not the same thing as changing his position. <I>San Francisco Chronicle</I> reporter Mark Sandalow reviewed more than 200 Kerry public statements on Iraq over the past two years, revealing that Kerry is not the "flip-flopper" on Iraq that Bush's campaign makes him out to be.</P>
<P>"As foreign policy emerged as a dominant issue in the Democratic primaries and later in the general election, Kerry clung to a nuanced, middle-of-the road--yet largely consistent--approach to Iraq," wrote Sandalow. "Over and over, Kerry enthusiastically supported a confrontation with Saddam Hussein even as he aggressively criticized Bush for the manner in which he did so.</P>
<P>"Kerry repeatedly described Hussein as a dangerous menace who must be disarmed or eliminated, demanded that the U.S. build broad international support for any action in Iraq and insisted that the nation had better plan for the post-war peace." Kerry's program for "victory" in Iraq (in his words) is a well-worn litany: more international support, training more Iraqi forces, increasing development aid and elections.</P>
<P>In response to Kerry's proposals, Bush actually told the truth: the Bush administration was already doing all of the things Kerry proposed. So no one should believe that Kerry and Bush have any substantive differences on Iraq.</P>
<P>Kerry is still the same candidate who insisted he would have authorized the war despite knowing that Iraq had no weapons of mass destruction. Within the political establishment that the Democrats and Republicans rule, there is a consensus on Iraq, the "war on terrorism" and other key issues in U.S. foreign policy.</P>
<P>But within the wider agreement on goals and aims, there is room for disagreement. This is especially true during election season, when candidates and parties accentuate even miniscule differences to appeal to their respective voting bases.</P>
<P>As Andrew Bacevich puts it in his excellent book <I>American Empire</I>, "Through tacit agreement, the two major parties approach the contest for the presidency less as an opportunity for assessing U.S. policies abroad than for striking poses--a hallowed and inviolable bit of political kabuki."</P>


