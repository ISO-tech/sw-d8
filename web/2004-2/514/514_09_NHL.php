<P><font face="Times New Roman, Times, serif" size="5"><b>NHL takes shots at players' union</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Stuart Easterling</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 1, 2004 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">CLAIMING MILLIONS of dollars in losses, the National Hockey League (NHL) locked out its unionized players on September 15. Like so many employers seeking concessions from unions, the NHL has cried poverty, and some team owners have threatened bankruptcy. </P>
<P>Yet 10 of the NHL's 30 teams are owned by billionaires. Another five are owned by people who were billionaires before the tech-stock bubble burst. Another seven are owned by members of the "Forbes 400" or the "Canada 100" lists &#151; the wealthiest people in these countries.</P>
<P>The NHL claims that pro hockey is struggling for a fan base. Yet the league set a record for attendance in the 2001-02 season, and the league admits that revenues have been going up. Nonetheless, the owners have been preparing for a lockout for years, building up a war chest of $300 million in order to ride out the dispute.</P>
<P>One team president declared that the league is willing to lock out players for an entire year if necessary. As part of its public relations campaign for concessions from the players, the league released a report earlier this year claiming that teams lost $273 million in the 2002-3 season.</P>
<P>But when the players' union completed a detailed review of four of the teams in the report, it found $52 million in unreported hockey-related revenue for just these teams. In other words, the teams "cook the books"--just like so many other businesses.</P>
<P>Even the NHL's vice president admits that some teams are "philosophically opposed" to accurately reporting their revenue. One independent analyst described the report as "a conscious...sleight of hand, designed first, last and always to support the NHL's claims, not test them."</P>
<P>Despite these criticisms, the teams will not open their books to the union or the media. One team was even fined by the NHL for revealing its finances to an independent accountant.</P>
<P>Meanwhile, the players are offering huge concessions. Right now, they aren't even paid their salaries during the playoffs--while owners, of course, still collect revenue. Players are controlled by their original team until they turn 31.</P>
<P>And they've offered an across-the-board salary reduction of 5 percent, which entails $100 million in concessions. They've also proposed a $60 million cut in rookie salaries, and a revenue-sharing plan to benefit lower-revenue teams.</P>
<P>All this isn't enough for the owners. They want a contractual limit on player salaries--a salary cap--to keep themselves from bidding too high for good players. Meanwhile, other NHL and team employees are getting the shaft. The NHL office has laid off half its staff in order to ride out the lockout. Other teams have taken similar measures, rather than take any extra money out of the pockets of the owners.</P>
<P>The media often points the finger at the players for not giving up more. But this isn't happening because the players are paid well to play hockey. It's happening because the owners want more, and they're willing to shut down the sport to get it. </P>
<P>Hockey players are basically entertainers, like successful actors or musicians. Like all top-level entertainers, they often fight with their employers to get more of a very large pie. But in the end, any employer publicly beating any union, even one of hockey players, is not a good thing for workers in general.</P>


