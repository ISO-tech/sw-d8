<p><a name="Global"></a><font face="Times New Roman, Times, serif" size="4"><b>Global justice</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--Top finance officials from the Group of Seven club of rich countries will meet October 1 to discuss measures that affect the lives of millions around the world. And on October 2 and 3, the global loan sharks of the World Bank and International Monetary Fund (IMF) will gather to figure out how best to serve these powerful governments.</P>
<P>Nowhere in these meetings will the victims of the "Washington model" of neoliberalism get their say. That's why global justice organizations are calling for demonstrations to protest the meetings--and draw attention to the real issues.</P>
<P>In 2002, developing countries received $58 billion in loans and development "aid." Yet that same year, more than five times that amount was drained out of the world's poorest countries--in payments to service their unpayable debts. Demonstrators want to point out the connections between the neoliberal agenda and the disaster of Washington's war on Iraq--with a protest that will gather at Arlington National Cemetery and then march to the White House.</P>
