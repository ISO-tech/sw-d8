<p><a name="Nader"></a><font face="Times New Roman, Times, serif" size="4"><b>News from the Nader campaign</b></font><BR>
By Paul Dean and Lee Sustar</P>

<P><font face="Times New Roman, Times, serif" size="3">RALPH NADER hit the campaign trail in late September, celebrating a ballot access victory in Maryland while vowing to challenge his removal from the ballot in Oregon. The Oregon Supreme Court on September 22 upheld a decision by the Oregon Secretary of State--a supporter of John Kerry--disqualifying thousands of signatures.</P>
<P>Although there is going to be an appeal to the U.S. Supreme Court, time is running out as ballots have to be printed shortly. "This is a sad day for democracy in America," said Nader. "It is evident that our independent presidential campaign has greatly stressed a corrupt exclusionary system, and that the Democratic Party will stop at nothing to deny voters the opportunity to vote for our candidacy.</P>
<P>"They would rather limit voters' choices--in an attempt to force them to vote for a candidate they do not believe in and do not support or stay at home in disgust. The anti-democratic approach of the Democratic Party is weakening a democracy already rendered anemic."</P>
<P>Across the country, Nader appeared with his running mate, Peter Camejo, at a rally at the University of Maryland, where the newly formed Populist Party has endorsed their candidacy. More than 750 people turned out for the event despite a protest called by Kerry supporters.</P>
<P>Camejo got loud applause for his sharp condemnation of the war and occupation of Iraq--and big laughs at the expense of Kerry. He called the 2004 race "the most peculiar election in U.S. history" because Kerry supporters claim that Kerry is lying when he supports the occupation of Iraq and takes other conservative positions.</P>
<P>Nader also spoke at length about U.S. foreign policy--not only about Iraq, but the unquestioning support for Israel by both Republican and Democratic politicians. Urging the largely student audience to commit themselves to social change, he said that "if you're not into politics, politics will be into you in a most disagreeable way."</P>

