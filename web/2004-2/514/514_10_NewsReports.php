<P><font face="Times New Roman, Times, serif" size="4"><b>Housing is a right</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Nathan Fuller</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">MADISON, Wis.--A rally to "Save Our Services" in Dane County attracted more than 200 participants September 23. Members from groups at the University of Wisconsin, including the Madison Warming Center Campaign, voiced concern over deteriorating services to the poor and handicapped. </P>
<P>In a city with an estimated 2,000 homeless people on any given night, 700 of whom are children, the city has just 300 shelter beds. This means that more than 400 children are forced to sleep on the streets! </P>
<P>With a vacancy rate of 6.5 percent in Madison, and a Democratic mayor who is pushing a "business-friendly" image, this injustice must not be allowed to continue. As the city gets set to vote on next year's budget on October 1, it's important that we continue letting local politicians know that people are more important than corporations, and that we will not stop fighting until all of our human rights are accounted for.</P>

