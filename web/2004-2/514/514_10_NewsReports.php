<p><a name="DR10"></a><font face="Times New Roman, Times, serif" size="4"><b>Justice for the Death Row 10</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Noreen McNulty</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--The Campaign to End the Death Penalty held an informational picket  September 24 at Cook County Courthouse for members of the Death Row 10, victims of Chicago police torture. Supporters then attended the hearings of Cortez Brown, Derrick King and Ronald Kitchen. All three were previously on death row based on coerced confessions and are now serving life without the possibility of parole.</P>
<P>In Cortez Brown's case, the prosecution has moved to dismiss his post-conviction petition, despite the fact that new information related to his allegations of torture has never been heard in court. At a press conference, activists questioned why the office of Illinois Attorney General Lisa Madigan is pressing for this petition to be dismissed. "It is time for Illinois Attorney General Lisa Madigan to stand up for those long oppressed by beatings, torture at the hands of the police, wrongful conviction and unjust imprisonment. It is time to free Cortez Brown," said Rev. Larry Turpin.  </P>
<P>On the same day, the Peoples Law Office filed in federal court to depose Mayor Richard Daley and former police commander Jon Burge for a civil lawsuit against the city of Chicago Citing a memo from 1985, lawyers wish to question Daley about what he knew about the allegations of torture.</P>

