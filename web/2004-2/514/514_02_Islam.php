<P><font face="Times New Roman, Times, serif" size="4">Look who the Feds are picking on now...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>New victim of "no-fly" list</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 1, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IS THE man who wrote the song <I>Peace Train </I>a dangerous militant with ties to terrorists? That's what the Bush administration's witch-hunters would like us to believe.</P>
<P>Last week, Yusef Islam, the singer formerly known as Cat Stevens, was on a flight with his daughter from London to Washington, D.C., when authorities diverted the plane in mid-flight to Bangor, Maine. According to the Transportation Security Administration, Islam (who became a Muslim in the 1970s) was on the government's "no-fly" list for those suspected of ties to terrorists.</P>
<P>Once in the U.S., Islam was separated from his daughter and interrogated. Then he was deported to London and barred from re-entering the U.S.</P>
<P>Islam says he was stunned. "The amazing thing is that I was not given (and have still not been given) any explanation whatsoever as to what it is I am accused of, or why I am now deemed an apparent security threat--let alone given an opportunity to respond to these allegations," he said in a statement. "I was simply told that the order had come from 'on high'...</P>
<P>"I am a man of peace and denounce all forms of terrorism and injustice; it is simply outrageous for the U.S. authorities to suggest otherwise. I have dedicated my life to promoting peace and understanding throughout the world." Exactly why the government threw Islam out of the country remains a mystery--especially considering that he visited the U.S. as recently as May without any problems.</P>
<P>Some government officials hinted that Islam supports charities that aid Palestinians living under Israeli occupation--and that some of the money might have gone to "terrorists." Others speculated that the singer's criticism of Bush's wars in Afghanistan and Iraq might have got him booted.</P>
<P>But in the end, it may be another embarrassing mistake for the Feds. According to <I>Time</I> magazine, there is no "Yusuf Islam" on the no-fly registry (which includes more than 19,000 names)--but there is a "Youssouf Islam." </P>
<P>It wouldn't be the first mistake. In recent months, Sen. Ted Kennedy has had problems flying because his name was reportedly on the list.</P>


