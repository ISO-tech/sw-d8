<font face="Times New Roman, Times, serif" size="4"><b>Bugging the French </b></font><p>

<font face="Times New Roman, Times, serif" size="3">A BOOK published in France says that the U.S. government regularly monitored the phone calls of French President Jacques Chirac. The book says several sources reported the surveillance.<p>
"The relationship between your president and ours is irreparable on the personal level," a U.S. official is reported to have told a senior French military official. "You have to understand that President Bush knows exactly what President Chirac thinks of him."<p>
Surveillance was possible because Chirac rarely uses secure phone lines, except in scheduled calls to world leaders. The book lends further credibility to reports that the United Nations is routinely bugged by larger powers to monitor diplomatic conversations. In the run-up to the Security Council sessions on Iraq in early 2003, French officials and members of other European delegations had to meet in an anti-bugging glass cage owned by the Germans in order to avoid their conversations being monitored.<br>
--<b>BBC News, </b>October 5, 2004<p>

<font face="Times New Roman, Times, serif" size="4"><b>The not-so-friendly skies</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IS THE Bush administration trying to bump off the White House press corps? That's the conclusion some journalists are drawing after the White House travel office signed a deal with airline company Primaris to fly the journalists to Bush events.<p>
It turns out that the two-month-old company has only one airplane--which is 15 years old and was damaged a decade ago in a hailstorm. There's also the fact that the company's president twice had his pilot's license revoked for flying an "unairworthy" aircraft.<p>
On a recent flight, a television news producer was disconcerted to feel something fall in his lap on takeoff: a sheared-off screw of unknown origin. On another flight, a jetway rammed the plane, cracking the plastic inside of a forward door, which was patched...with duct tape. <p>
On yet another flight scheduled to leave from Akron, Ohio, the pilot announced some "bad news": The starter in the starboard engine would not start. While a mechanic removed engine parts and put them on the tarmac, and some White House aides were quickly put on a cargo plane back to Washington, the press corps was taken to a nearby bar until a backup plane, flown by ATA, got them on their way, six hours later. <br>
--<b><i>Washington Post,</i> </b>October 12, 2004<p>

