<p><a name="Berkeley"></a><font face="Times New Roman, Times, serif" size="4"><b>University of California-Berkeley</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Kathryn Lybarger, AFSCME Local 3299</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BERKELEY, Calif.--Some 200 members of the American Federation of State, County and Municipal Employees (AFSCME) Local 3299, along with students and other supporters from the University of California (UC)-Berkeley campus and community, rallied October 15 to demand a better contract. At one point, the rally moved to take over a major intersection at an entrance to the campus, where 16 members sat down for several minutes before being arrested.<p>
The rally highlighted a study done by Berkeley graduate students that exposes shamefully low wages and shoddy working conditions on the supposedly "progressive" campus. Local 3299 represents over 7,000 of the UC system's low-wage service workers, about a quarter of whom earn less than $10 an hour, and well over three-fourths of us don't make the minimum considered necessary to get by in California's expensive urban centers.<p>
AFSCME workers have been without a contract since the old one expired June 30, and the university has offered us nothing so far. Yet the UC system managed to find the money to give Berkeley's and San Diego's new chancellors each a $70,000 per year raise and similar pay increases for three other top UC executives.<p>
The rally was planned to coincide with the local's statewide rank-and-file leadership conference and UC Berkeley's homecoming. The mood at Friday's rally was loud and feisty. UC is heading for a fight.<p>
