<p><a name="Health"></a><font face="Times New Roman, Times, serif" size="4"><b>Group Health</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Kate Johnson</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--About 400 Group Health Cooperative workers rallied and marched October 16 to demand affordable health care.<p>
Service Employees International Union (SEIU) Local 1199NW--which covers LPNs, medical assistants, social workers, janitors and several other workers--has been in negotiations for a new contract for a year and a half since their last contract expired.<p>
At the heart of the negotiations are Group Health's proposed cuts in health care. Currently, SEIU members pay no premiums and $5 co-pays for visits and prescriptions--but Group Health (GH) wants employees to pay 11-12 percent of staff health care costs.<p>
The rally was a response to failed negotiations following a weeklong strike in August. "We wanted to show them [GH] that we're not going to back down," one nurse told <i>Socialist Worker</i>. "We are less than 1 percent of their total budget. This isn't about what they can afford; it's about Group Health trying to make a few extra bucks," she said.<p>
Workers at the rally were energetic and ready to fight. But the majority of the speakers at the rally were politicians running for office. SEIU president Andy Stern made an appearance, urging workers to vote for John Kerry--and union leaders have decided to cease all attacks on GH until after the elections. This is mistaken. The only way to win is to keep the pressure up.<p>

