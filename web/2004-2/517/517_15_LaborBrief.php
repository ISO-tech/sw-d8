<font face="Times New Roman, Times, serif" size="4"><b>San Francisco hotels</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Abraham Gomez and Adrienne Johnstone</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Hotel workers set to end their two-week strike and return to work last Wednesday were instead met with a lockout by hotel management here. The hotel owners' organization, the Multi-Employer Group (MEG) insists the lockout will continue until the union, UNITE HERE Local 2, drops its demand for a two-year contract.<p>
Local 2 wants a contract that expires at the same time as its counterparts in Los Angeles and Washington, D.C., where UNITE HERE members have also authorized strikes. Meanwhile, another 10,000 UNITE HERE workers are on strike at hotels and casinos in Atlantic City.<p>
The battle in San Francisco began after the union members voted 97 percent to authorize a strike for wages, reduced workloads and a freeze on insurance premiums. Local 2 President Mike Casey publicly argued that a two-week strike at only four hotels would show the bosses that they were both serious about their demands, but also willing to "work with management" in order not to hurt profits or disrupt tourism.<p>
The MEG responded by locking out workers at 10 hotels, putting 4,000 workers on the picket line.<p>
At first, picket lines were spirited and well-organized, with hundreds of workers blowing whistles and beating drums, confident that they could win. Yet the bosses went on the offensive, getting police to crack down on the noisy picketers. Casey claimed that "the union had ordered the noise toned down even before the police warned them," adding, "we'll find other ways of productively and profitably letting off steam." This has had a depressing effect on the picket lines. <p>
Much is at stake in this strike. Last summer, UNITE HERE was formed from a merger of two unions as part of the New Unity Partnership strategy of a break from the passive, accommodating approach of other unions. The Local 2 strike is a test of this strategy.<p>
Unfortunately, by arguing "what's good for the industry is good for us," the union weakened its own position from the beginning. Moreover, many hotel workers are only learning about their own struggle through the few local press bulletins or meetings where workers can discuss what to do next. <p>
Many workers express the willingness to press for more aggressive strategies, but there's no way to bring these ideas forward within the union. Striking workers need to find ways to bring the noise back--and disrupt business as usual in the hotels.<p>
