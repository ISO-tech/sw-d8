<font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Guilt trippers for Kerry</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 22, 2004 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">ON OCTOBER 28,<i> Left Business Observer</i> editor Doug Henwood will air an interview with left-wing author and activist Tariq Ali on New York's WBAI radio.<p>
In a previous interview with Henwood back in August, Ali excoriated those on the U.S. left who have not joined the "Anybody But Bush" camp. "This is an argument you can have from the luxury of your sitting room or kitchen in the United States, but this particular regime has taken the lives of at least 37,000 civilians in Iraq," Ali said. "For them, it's not an abstract question."<p>
Henwood promises that in the upcoming interview, Ali will release yet more invective against those who disagree that "the importance of defeating Bush...means electing Kerry." According to Henwood, Ali will tell "American leftists" to "stop emulating the Bush administration and think not so much of their preferences, but those of the outside world instead." <p>
Yet Ali himself argued, as recently as February 14 in Britain's <i>Guardian </i>newspaper that, "regardless of which party is in power," the U.S. cannot "afford a setback in Iraq" because it "would be a major blow against the 'empire' and weaken its ability to control other parts of the world."<p>
Those on the U.S. left who continue to refuse to support Kerry do so not because we cling to "abstract" political purity, but precisely because--like Ali back in February--we understand that both the Democratic and Republican Parties share a common goal in promoting the interests of American "empire" and its "ability to control other parts of the world."<p>
Both candidates stand for more war. Why pretend otherwise? Kerry doesn't. He has repeatedly stated about Iraq, "I want victory. I want to win. And I have a better plan to win than George Bush does." These cannot be interpreted as the words of a peace candidate, despite the tremendous efforts of Kerry's left apologists to imply otherwise. <p>
Kerry is perhaps more virulent than Bush in supporting the Israeli occupation of Palestine, calling Israel "the only true democracy in the Middle East"--despite its ongoing occupation of Palestinian land, and apartheid oppression and ethnic cleansing of Palestinian people. As Kerry wrote in February, "In this difficult time, we must again reaffirm we are enlisted for the duration--and reaffirm our belief that the cause of Israel must be the cause of America."<p>
Kerry also stated during the recent presidential debates, "I support the PATRIOT Act," noting that his running mate, John Edwards, had helped draft it in the Senate. Casting aside Bush's efforts to label him a "liberal," Kerry went on to denounce the Bush administration for allowing "Middle Eastern people" to enter the U.S. without scrutiny.<p>
Zogby opinion polls conducted this summer showed that 20 percent of Arab Americans in the closely contested states of Michigan, Pennsylvania and Ohio supported Ralph Nader in a three-way presidential race with Bush and Kerry. This significant support for Nader among Arab Americans contradicts the caricature of the self-absorbed and elitist U.S. left that Tariq Ali lectures for refusing to support Kerry. <p>
As Palestinian activist and scholar Naseer Aruri wrote recently in the Egyptian <i>Al-Ahram Weekly</i>, "[V]oting for the 'lesser evil' is placing the [Arab-American] community at the mercy of both parties, who can only welcome the free and generous support for their candidates with no strings attached." <p>
Nader's platform, Aruri continued, "is the only one that responds to Arab-American interests and positions on Palestine, Iraq, civil liberties and worldwide respect for international law." Far from a "wasted vote," Aruri argued, a strong showing for Nader "would constitute the initial necessary investment in a long and continuing process designed to keep all future candidates apprised of the actual worth of the Arab-American vote."<p>
Ali would do well to revisit his own comments last February, when he observed, "Iraqis have one thing of which they can be proud, and of which British and U.S. citizens should be envious: an opposition." A left third party can only be built as a political expression of a movement against U.S. imperialism and a rejection of both its war parties.<p>

