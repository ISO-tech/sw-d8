<font face="Times New Roman, Times, serif" size="4">Schwarzenegger vetos immigrant drivers' licenses</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Governor Scapegoater</b></font><p>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 22, 2004 | Page 12</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
As governor of California, Arnold Schwarzenegger has been able to sell himself as a moderate and a "compassionate conservative." <p>
Indeed, this image and his popularity wasn't lost on the leaders of the Republican Party who opportunistically showcased him at the Republican National Convention. On stage, Schwarzenegger conveyed a message designed to attract immigrants and tout the "American dream." He said, "To my fellow immigrants listening tonight, I want you to know how welcome you are in this party." <p>
Schwarzenegger showed his true anti-immigrant position two weeks ago, however, when he vetoed AB 2895, which would have allowed undocumented immigrants to obtain driver's licenses. <p>
Many immigrant rights groups and community organizations have been campaigning around this issue for years. They rightly argue that millions of immigrants who work in low-wage jobs as agricultural laborers, hotel janitors and maids, restaurant workers and food-processing plant operators need driver's licenses in order to drive to work, to drive their children to school--and, thus, just to make an ordinary living. Indeed, living in such impoverished and unsafe conditions is more of a nightmare than a dream. Along with low wages, the large majority of immigrants are subject to unsafe working conditions and the constant threat of deportation. <p>
Schwarzenegger's veto of the bill should not come as any surprise. He actually overturned the passage of a similar bill when he became governor, citing the threat it posed to national security. The latest version of the bill would have required background checks against terrorist watch lists and detailed documentation, such as birth certificates, that many immigrants simply do not own. Thus, it was questionable how many immigrants would actually participate in the program, even if the bill were passed! <p>
The tone set by the "moderate" Schwarzenegger has given cover to stronger anti-immigrant forces. For instance, the California Republican Assembly, a conservative group of Republicans, announced that it would start collecting signatures for a 2006 ballot measure to prevent illegal immigrants from receiving government benefits, including licenses. <p>
But if denying immigrants government benefits sounds familiar, it is because Democrats have helped popularize the idea. Former President Bill Clinton, for instance, attempted to pass a similar provision in his welfare plan. Former Democratic Gov. Gray Davis refused to sign the original driver's license bill until his last few days of office last year--in a desperate attempt to save his governorship during the California recall election. And Schwarzenegger overturned the original bill with the support of a Democrat-controlled legislature! <p>
The current impasse over driver's licenses for immigrants really reflects the futility of relying solely on the Democrats and the legislative process. What is needed instead is a strong, independent grassroots movement comprised of unions, immigrant rights groups, health care advocates and others that challenge the priorities of the system. <p>
The opportunity exists. In as rich a state as California, progressives could provide a model for the rest of the country in how to attain social justice and more equality. Putting our resources into building the Nader-Camejo campaign--the only presidential ticket that favors immigrant driver's licenses, national health care and a living wage--is a great place to start. <br>
<b>Mike Gaeta, </b>Fresno, Calif.<p>

