<font face="Times New Roman, Times, serif" size="5"><b>White House book banner</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 22, 2004 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">ATTACKING ACADEMIC freedom is all in a day's work for Lynne Cheney.<p>
Following the September 11 attacks, the American Council of Trustees and Alumni--a group co-founded by the wife of the vice president--released a rabid assault on professors who dared to speak out against the U.S. bombing of Afghanistan. Now, Lynne Cheney is apparently behind the Education Department destroying more than 300,000 copies of a new printing of a parent's guide called "Helping Your Child Learn History."<p>
The 10-year-old guide is aimed at helping parents present historical information to their kids. But Cheney objected to the book because it mentions the National Standards for History--a set of guidelines developed by scholars in the 1990s to present a more balanced view of history.<p>
When they were created, Cheney led a campaign against the standards, claiming that they were not "positive" enough about American achievements--because they contained repeated references to the Ku Klux Klan and Sen. Joseph McCarthy, and mentioned escaped slave Harriet Tubman six times, while Paul Revere, Robert E. Lee and the Wright brothers went unmentioned. <p>
The furor over the standards led to their revision--and even most right-wing critics were satisfied. But not Cheney. Despite the fact that she holds no actual government position, her office was able to get the Education Department to "recycle" the guides--which cost more than $110,000 to print.<p>
"That's a pretty god-awful example of spending the taxpayers' money and also a pretty god-awful example of interference--intellectual interference," retired UCLA professor Gary Nash, co-chair of the effort to develop the National Standards for History, told the <i>Los Angeles Times</i>. "If that's not Big Brother or Big Sister, I don't know what is."<p>

