<font face="Times New Roman, Times, serif" size="4">Million Worker March draws union members and allies</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Organizing for labor rights and social justice</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 22, 2004 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">WASHINGTON--About 5,000 people rallied at the Lincoln Memorial October 17 in an effort to put forward trade-union, working-class and antiwar issues shortly before Election Day. The protest, called the Million Worker March (MWM), allowed for discussions barred from mainstream political debate and the unions, taking up everything from a living wage to a national health care system to the U.S. occupation of Iraq. <p>
Featured speakers--and key march organizers--included longstanding activists in the Coalition of Black Trade Unionists (CBTU) such as Clarence Thomas of International Longshore and Warehouse Union (ILWU) Local 10 and Brenda Stokely, president of AFSCME District Council 1707 in New York. Several speakers from unions and antiwar organizations gave militant speeches that roused the crowd, which included a large number of student activists from the East Coast.<p>
Although the AFL-CIO opposed the march as a distraction from the election, several major union bodies endorsed the event. Backers included the National Education Association, the largest labor organization in the U.S., and, in New York, the 100,000-strong AFSCME District Council 37 and the 30,000-member Transport Worker Union, which represents bus and subway workers.<p>
Yet most unions sent only a few dozen people at most, with the notable exception of International Longshoreman's Association (ILA) Local 1422 in Charleston, S.C., which filled two buses. This reflected the fact that virtually every union has thrown its resources and staff into putting John Kerry into the White House.<p>
While creating an independent platform for working-class politics before an election is a good idea in principle, the organizing effort began in the summer, which didn't provide much time to organize at the rank-and-file level. Union leaders were prepared to endorse the march, but not to back it with resources--which led to gap between the name of the event and the actual turnout. <p>
Moreover, scheduling the march on the eve of the elections inevitably raised the question of which candidates that labor should--or should not--support. For example, ILA Local 1422 President Ken Riley called on the crowd to "dump Bush," as did Martin Luther King III and others. Other speakers, such as the entertainers and activists Dick Gregory and Danny Glover, sidestepped the issue and gave motivational speeches instead.<p>
One speaker who did address labor's political future was Leo Robinson, a retired member of ILWU Local 10 and a CBTU activist. Robinson said that the organizers of the march would continue to meet in order to have "influence" in the 2006 midterm elections. The question of whether the labor left should support John Kerry and the Democrats on November 2 was left hanging. <p>
More generally, the march can be seen as part of the unfolding debate about labor's future that's taking place among the AFL-CIO's largest unions. While the Million Worker March didn't meet the hopes or expectations of its organizers, it raised important issues that need to be at the center of the discussion about how to reverse labor's decline.<p>

