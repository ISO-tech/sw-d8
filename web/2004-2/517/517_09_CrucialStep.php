<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A chance to take a "crucial first step"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 22, 2004 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">DURING THIS election year, the U.S. left's capitulation to the pro-war, pro-business Democratic Party is being made more palatable by a little Nader-bashing. Ralph Nader plays footsie with the right wing, goes the argument, so he shouldn't be seen as a left-wing alternative at all.<p>
But the fact that the Democrats spent millions to keep Ralph Nader and Peter Camejo off the ballot in a number of states speaks volumes about what audience their campaign speaks to.<p>
The major planks of the Nader-Camejo ticket are ending the war, support for a living wage and universal health care, and opposition to the USA PATRIOT Act. In spite of whatever limitations Nader might have--and he certainly has some, for example, his equivocal stance on immigrant rights--it is clear that the Nader-Camejo ticket represents a break from the Democratic Party <i>to the left</i>. The most important thing for us isn't that we find a perfect candidate to support in the presidential election, but that we take the opportunity to promote a political and organizational break with the Democratic Party.<p>
This is the great task that stands before the U.S. left. Karl Marx and Frederick Engels had much to teach on the value of the left and progressives running their own candidates.<p>
In 1886, the Central Labor Union in New York formed the Independent Labor Party of New York and Vicinity in order to participate in New York City's mayoral election. The new party chose single-tax advocate Henry George as its candidate.<p>
George himself wasn't from the labor movement. He was a middle-class populist who had recently written a popular book, <i>Progress and Poverty</i>, that attacked poverty and inequality, and advocated a single tax on landed property as a panacea to solve society's ills. In a hotly contested race in which the ruling class pulled out all the stops to prevent a labor-party victory, George came in second in a three-way race, with 31 percent of the vote.<p>
Writing from Europe, Engels was positive about the election. "In a country that has newly entered the movement, the first really crucial step is the formation by the workers of an independent political party, no matter how, so long as it is distinguishable as a labor party," Engels wrote. "That the first program of this party should still be muddle-headed and extremely inadequate, that it should have picked Henry George for its figurehead, are unavoidable if merely transitory evils. The masses must have time and opportunity to evolve; and they will not get that opportunity unless they have a movement of their own--no matter what its form, providing it is their own movement--in which they are impelled onwards by their own mistakes and learn by bitter experience."<p>
We must have a politically independent movement of our own--these are lessons we still must learn in the U.S. today.<p>
What about the argument that voting for a minority left candidate "steals" votes from liberals and helps the right wing into power? <p>
Here's what Marx and Engels said about it in the 1840s: "Even when there is no prospect whatsoever of their being elected, the workers must put up their own candidates in order to preserve their independence, to count their forces, and to bring before the public their revolutionary attitude and party standpoint. In this connection, they must not allow themselves to be seduced by such arguments of the Democrats as, for example, that by so doing they are splitting the Democratic Party and making it possible for the reactionaries to win. The ultimate intention of all such phrases is to dupe the working class. The advance which the proletarian party is bound to make by such independent action is infinitely more important than the disadvantage that might be incurred by the presence of a few reactionaries in the representative body."<p>
Misplaced support on the left for the Democratic Party has meant that our side remains disoriented and disorganized by elections. The Nader-Camejo ticket affords us that "first crucial step" to begin "counting our forces." The time is now to take advantage of it.<p>

