<font face="Times New Roman, Times, serif" size="5"><b>Laughing all the way to the bank</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 22, 2004 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE CORPORATE giveaways keep on coming. Just a week after George W. Bush signed another new round of supposed "middle class" tax cuts into law--which included $13 billion in new corporate tax breaks--Congress was at it again.<p>
On October 11, the Senate approved the misnamed "American Jobs Creation Act of 2004"--a sweeping corporate tax overhaul already approved by the House and soon to be signed by Bush--that will pad Corporate America's pockets with another $136 billion in tax giveaways.<p>
As MSN Money reporter Rick VanderKnyff commented, "Multinational corporations. Makers of ceiling fans, archery equipment, sonar fish finders and tackle boxes. Owners of NASCAR tracks, growers of tobacco and makers of movies. If you count yourself among one of the above groups, rejoice. You are a likely beneficiary of the fat new $140 billion corporate tax bill now on the president's desk." <p>
The bill is a response to tariffs imposed by the European Union after the World Trade Organization (WTO) ruled that U.S. tax breaks for American products amounted to illegal trade subsidies. The bill repeals those manufacturing tax breaks--but replaces them with plenty of others that are designed to withstand the WTO.<p>
In a stunning giveaway, the bill includes a provision that effectively cuts 3 percentage points off the top corporate tax rate--not only for traditional manufacturers covered by the original subsidy, but for architectural and engineering firms, timber companies, oil and gas drillers, movie studios and more. Multinational corporations, meanwhile, are treated to a one-year tax "holiday." Then there are the pro-business boondoggles like a $10 billion buyout for tobacco growers and rules allowing wealthy owners of sports teams to write off the full value of their teams in just 15 years. There's even a provision eliminating $44 million in tariffs on foreign-made ceiling fans--a gift to Home Depot, which sells half of all ceiling fans in the U.S.<p>
But if you're an average person, says VanderKnyff, "what you get in this feeding frenzy is mostly crumbs--plus the chance to be hassled by tax collectors who aren't even from the IRS." That's because one part of the bill would allow the IRS to get private debt collection agencies to collect overdue federal taxes from people accused of owing the government money--for a 25 percent cut of the collected debt. <p>
Despite the election-year promises of both parties to provide for the "middle class" and working people, this latest giveaway to Corporate America was the work of both Democrats and Republicans--who rushed to pack the pork into the bill to make their corporate backers happy. In the end, the Senate vote was 69 to 17--with both senators John Kerry and John Edwards not even bothering to show up.<p>
The next time Kerry tries to say that the "American Dream" is out of reach for too many workers in Bush's America, maybe someone should ask him why the Democrats helped rob working people once again in this latest round of corporate giveaways.<p>

