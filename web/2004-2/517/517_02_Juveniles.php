<font face="Times New Roman, Times, serif" size="5"><b>Sick face of the death machine</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 22, 2004 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">ONLY A handful of rogue nations have executed juvenile offenders since 2000. Iran, Pakistan, the Congo...and the United States.<p>
Finally growing queasy about keeping company with "failed states" and countries in the "axis of evil," the U.S. Supreme Court is hearing a case that could lead to a ban on capital punishment f juvenile offenders. <p>
Last year, the Missouri Supreme Court set aside Christopher Simmons' death sentence, arguing that "evolving standards of decency" rule out executing those who commit crimes while under the age of 18. In 2002, four Supreme Court justices held this view, writing in a dissenting opinion in a different case, "The practice of executing such offenders is a relic of the past and is inconsistent with evolving standards of decency in a civilized society." <p>
But Missouri's attorney general still wants to put Simmons to death--and appealed the Missouri high court's decision to the U.S. Supreme Court.<p>
The execution of juveniles highlights all the cruelties of the U.S. injustice system. For example, racism is a pervasive factor, just as it is in the death penalty and justice system in general. Of 72 juvenile offenders on death row, 40 percent are Black, and 21 percent are Latino.<p>
Fewer and fewer states tolerate the execution of juveniles. Since 1988, when the U.S. Supreme Court outlawed the execution of offenders ages 15 years and under, seven states have banned all juvenile executions. Only 19 states remain that allow for executing 16- and 17-year-olds. In 2002, when the Supreme Court banned executions of mentally retarded prisoners--on the same grounds of "evolving standards of decency"--20 states allowed for such executions. Meanwhile, surveys of jurors now find that even fewer jurors are comfortable with executions of juveniles than with executions of the mentally retarded.<p>
The European Union and other countries, including Mexico, Canada and Iceland, filed a brief with the court arguing that the U.S. should no longer flout the international consensus against the execution of juveniles. But some justices--in particular, William Rehnquist and Antonin Scalia--say this "international consensus" shouldn't have any bearing.<p>
In particular, Scalia's fanatical support for the death penalty seemed to defy basic legal argument. "Why pick on the death penalty? Why not say they're immune from any criminal penalty?" asked Scalia during hearings. "I don't see where there's a logical line." If a Supreme Court justice can't see the logic in laws that treat adults and children separately, why is he still on the bench?<p>
The truth is that Supreme Court doesn't base its rulings on the neutral application of timeless legal principles, but on a range of factors, including the general political climate. The anti-death penalty movement has helped to encourage more public doubt--and hopefully stands on the verge of striking another blow against the machinery of death.<p>

