<p><a name="War"></a><font face="Times New Roman, Times, serif" size="4"><b>Stop the war</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Andrew Jagunich</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--About 60 demonstrators picketed the Times Square Army recruiting station on October 14. At the event, sponsored by United for Peace and Justice, protesters carried cardboard coffins draped in black, while others carried signs with the number of civilians killed in Iraq since the war began.<p>

