<font face="Times New Roman, Times, serif" size="4"><b>Fight for immigrant rights</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Sarasvati Ting</b></font><p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES--Some 3,000 people took to the streets of East Los Angeles on October 16 to demand equal rights for immigrants.<p>
The California Coalition for Immigrant Rights and Against the Raids organized the march to denounce the anti-immigrant raids conducted by the Border Patrol and Immigration and Customs Enforcement in Southern California. The march also commemorated the 10-year anniversary of the 100,000-strong march against anti-immigrant Proposition 187.<p>
Felipe, who brought his whole family to Saturday's march, also organized a contingent from South Central LA in 1994 to march against Prop 187. "When we arrived downtown and we saw the big protest and joined up with it, that was the best feeling I've ever had in my life," he said.<p>
Groups came together from across Southern California to make the demonstration a success. Meanwhile, a smaller demonstration of about 800 was organized by the ANSWER coalition at the same time in another part of downtown LA. This disunity was unfortunate, but ultimately didn't take away from the main march's success.<p>
"We have to send this message to the politicians of both parties," said Mexican-American Political Association President Nativo Lopez, who recently reregistered from Democratic Party to the Green Party. David Cid, a middle school teacher in East LA spoke out against military recruiters invading high schools. Peter Miguel Camejo, vice presidential running mate with Ralph Nader, also spoke.<p>
Other speakers, like UNITE HERE Local 11 President Maria Elena Durazo, supported Democrat John Kerry. "We have to go out and vote, not just sit home in front of the TV," Durazo chastised protesters. "That's how we're going to kick Bush out of the White House. That's how they're going to respect us!"<p>
But we won't get respect, or any of the march's demands, by voting for a Democrat. We need a strong immigrant rights movement to beat the attacks on immigrants in the name of the "war on terror." As a hotel worker at the march put it, "We are here to demand amnesty and to say no to discrimination. We are workers, not terrorists."<p>
-- In the Southern California suburb of <b>San Ysidro</b>, which borders Mexico, more than 600 protesters--from both sides of the border--marched on October 2 to mark the 10th anniversary of "Operation Gatekeeper." Because of Operation Gatekeeper's lockdown of Southern California's border, thousands of immigrants have been forced to cross through the deserts east of Southern California. An estimated 3,000 people have died in the process.<p>
<i>Ty Coronado and Gillian Russom contributed to this report.</i> <p>
