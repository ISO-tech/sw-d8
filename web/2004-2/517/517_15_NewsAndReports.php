<p><a name="Nader"></a><font face="Times New Roman, Times, serif" size="4"><b>News from the Nader campaign</b></font><p>

<font face="Times New Roman, Times, serif" size="3">"IN NO European country would you have a candidate running for president like Kerry, where the majority of people who plan to vote for him don't agree with him," independent vice presidential candidate Peter Camejo told an 800-strong crowd at the University of California (UC)-Davis.<p>
Davis was one of the stops on independent presidential candidate Ralph Nader's tour of Northern California universities, which also included UC-Berkeley and San Francisco State University (SFSU), on October 11. Paraphrasing American socialist Eugene Debs, Nader argued, "I'd rather vote for a candidate I want that loses, than vote for a candidate I don't want that wins."<p>
At SFSU, Camejo and Nader were greeted by over 1,000 enthusiastic students and local activists. Nader called for withdrawal of U.S. troops as well as U.S. corporations from Iraq. He also compared the emphasis that both candidates place on fighting terrorism to the tactics of the McCarthy era. "Terrorism is the new Communism," Nader said.<p>
Also speaking at the event was Matt Gonzalez from the Green Party, who nearly won San Francisco's mayoral run-off election last year.<p>
-- In <b>Seattle, </b>Nader and South Central Los Angeles Green Party activist Donna Warren spoke to more 600 people at the Seattle Center on October 10. "Ronald Reagan unleashed poverty that was responsible for my son's death in the '80s," Warren said. "Now I see these mothers today who have lost their sons in Iraq."<p>
Nader hammered home the bipartisan nature of the war in Iraq and the attacks here at home on workers' living standards. "We can't leave our fight up to the Democrats because they are funded by the same corporate money, and you don't bite the hand that feeds you," he said.<p>
That week, Camejo also spoke at Seattle Central Community College, where 100 people turned out. He pointed out the hypocrisy of the claim that he and Nader have allied with Republicans--when the Kerry campaign has received millions of dollars from pro-Bush sponsors.<p>
<i>Jeff Boyette, Tonya Douraghy, Jesse Hagopian and Kolya Ludwig contributed to this report.</i> <p>
