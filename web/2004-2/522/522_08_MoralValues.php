<font face="Times New Roman, Times, serif" size="4">Why didn't the Democrats put up a real fight?</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The "moral values" hype</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 3, 2004 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
Since the election, we've been bombarded by the media with talk about "moral values" and, in particular, about gay marriage being the defining issue that drove people to the polls en masse to re-elect Bush. Don't believe it. <p>
According to CNN exit polls, 22 percent of voters said that moral values was the number one issue they cast their vote on. What isn't mentioned though, is that 78 percent saw other issues as more important. For those voters, the war on terror, Iraq and the economy topped the list. <p>
Of the 22 percent that voted on the basis of moral values, 80 percent voted for Bush, 18 percent for Kerry and 1 percent for Nader. By my calculations, there were 200 million eligible voters in the election. About 116 million people actually voted. Of those, almost 25 million based their vote on moral values, and of those, 20 million voted for Bush. In other words, 10 percent of eligible voters voted for Bush because of "moral values." <p>
I wonder when and how "moral values" became synonymous with gay marriage, which is what the "I'm-moving-to-Canada" crowd is blaming Bush's victory on. A September Fox news poll found that only 4 percent of those surveyed said that gay marriage was the most important issue to them. <p>
So what accounted for the big surge in homophobia? And how do we square that with all of the polls consistently showing that the majority of Americans support either marriage rights or civil unions for gay couples? <p>
We can't. While it is true that 11 states passed anti-gay ballot measures--without question a horrible defeat--we can't use that to paint a picture of a country full of homophobes. <p>
First, conservatives specifically chose states where they expected they could win these ballot measures and poured resources into them. Second, not everyone who voted for the ballot measures voted for George Bush. In Ohio, Bush won by a slim majority, yet one of the worst gay marriage bans won by a much wider margin. <p>
So Kerry voters were voting against gay marriage, too! And why wouldn't they, when both candidates opposed equal marriage, and the so-called gay rights movement silenced itself so as not to alienate conservative "swing voters"? <p>
The tragedy is that with no confident argument being made from the left to defend equal marriage, people never got to hear an argument as to why they shouldn't oppose it. <p>
Liberals and the left need to stop blaming the American working class for this election and take a long hard look in the mirror. Only if we understand how and why the Anybody-But-Bush/lesser-evil strategy failed us can we figure out how to move forward.<br>
<b>Steve Trussell, </b>Boston<p>

