<font face="Times New Roman, Times, serif" size="4">Sent to die in Bush's war for oil and empire</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bring them home now!</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 3, 2004 | Page 1</font><p>

<font face="Times New Roman, Times, serif" size="3">THE U.S. military has reduced Falluja to a pile of rubble. "They used everything--tanks, artillery, infantry, poison gas," resident Abu Hammad told reporters. "Falluja has been bombed to the ground."<p>
The full extent of the horror is still hidden to many in the U.S.--thanks to the see-no-evil, pro-war propaganda pumped out by the U.S. media.<p>
But the Pentagon is ready to inflict more death and destruction. And that means finding fresh troops to deploy from the U.S.--increasingly, National Guard members and reservists snatched away from civilian life.<p>
At the Do&ntilde;a Ana training camp in New Mexico, Army National Guard troops from California expressed their anger at the sped-up deployment by fleeing the camp during the Thanksgiving holidays--despite armed guards and barbed wire used to lock them down. "I feel like an inmate with a weapon," Corporal Jajuane Smith, a six-year Guard veteran, told the <i>Los Angeles Times.</i><p>
Staff Sgt. Lorenzo Dominguez was even more blunt in his criticism of poor training and lack of equipment--despite the risk of being punished for speaking out. "Some of us are going to die there, and some of us are going to die unnecessarily because of the lack of training," he said. "So I don't care. Let them court-martial me. I want the American public to know what's going on. My men are guilty of one thing: volunteering to serve their country. And we are at the end of our rope."<p>
Bitterness is growing among troops already in Iraq. Like the 18 soldiers of the 343rd Quartermaster Company, who were disciplined for refusing to carry out a dangerous-verging-on-suicidal mission in October. The Army Reserve company was ordered to transport fuel--in unarmored, run-down vehicles without an armed escort along a 200-mile route known for heavy fighting.<p>
And for what? The fuel they were transporting had already been rejected as contaminated at another fuel depot. Now, the 18 face military discipline, prison sentences and courts-martial.<p>
Even soldiers who support the war can be driven to resistance by such conditions--and the pointlessness of orders from the brass. During the Vietnam War, the refusal of troops to carry out missions was critical to ending the war--by forcing Washington's war makers to confront the fact that they were losing control of their own military.<p>
But in order for soldiers' doubts to be turned into action, the antiwar movement must be strong enough to project the real story of what the war on Iraq is all about. Our presence must be strong enough to convince more people that the U.S. war for oil and empire won't make anyone safer--but will instead only lead to more deaths, of Iraqis and U.S. soldiers alike.<p>
We need greater numbers, and we need a clear analysis--so soldiers feel that they're part of a movement that rejects this war. Bring the troops home now!<p>

