<font face="Times New Roman, Times, serif" size="5"><b>Immigrants under attack across Europe</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 3, 2004 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
In September, Swiss voters rejected two ballot initiatives aimed at simplifying the naturalization process for second- and third-generation immigrants. <p>
Far from representing a true right to citizenship, these two initiatives offered some modest gains within the framework of a very restrictive set of immigration laws. The initiatives were supported by a wide array of groups. Unions and left-wing parties, bosses' organizations and right-wing government parties led a joint campaign of support. <p>
One notable exception was the Centrist Democratic Union (UDC), the party of Christoph Blocher, federal counselor in charge of justice and the police. It is this xenophobic, right-wing party that dictates policy regarding immigration laws. The UDC's rhetoric systematically stigmatizes asylum seekers and undocumented immigrants, accusing them of being profiteers or, worse, delinquents. <p>
This aims to foster racist and xenophobic sentiments among a population that is under constant social pressure. But such a strategy is indispensable to strengthening the kind of discriminatory and repressive immigration laws advocated by the UDC. <p>
An advertisement that appeared repeatedly in the major daily newspapers during the campaign illustrates how the UDC operates. This racist ad portrayed Muslins as "undesirables" and "invaders," claiming that by the year 2040, Muslims would constitute 70 percent of the Swiss population--even though they only represent less than 3 percent today. The Committee Against Mass Naturalizations that issued this ad is presided over by a member of the UDC's national council. <p>
On September 26, this rhetoric gained resonance among a significant layer of people, many of whom accept such chauvinistic and reactionary ideas as a way to address their own insecurities and concerns.<p>
It is important to place the defeat of these two initiatives in the context of a hardening of immigration policy, in Switzerland as in the rest of Europe. European laws are becoming more and more restrictive overall, while, in practice, each country faces a different set of economic and demographic needs. <p>
In Switzerland, the parliament is in the process of revising immigration laws in order to develop a more coherent policy that responds to employers' needs. The first instrument at their disposal will be a revision of the Asylum Law. This would allow Switzerland to refuse 98 percent of asylum requests and create thousands more undocumented immigrants who will be at the mercy of employers. <p>
The second tool they envision is a law governing foreigners that will create more difficult conditions for bringing families together, make greater use of short-term work permits and so on. This is nothing more than the codification of a discriminatory model for admitting and exploiting immigrants, depending on their national origin and their work qualifications. <p>
The UDC dictates this policy, with other right-wing parties in tow. But far from denouncing this discriminatory policy that degrades living and working conditions for immigrants and all workers, so-called "left government" parties and unions alike have sanctioned it by abandoning their base. Thus, the Swiss Socialist Party--the main party of this "governmental left" and the right arm of the trade unions--gave its approval to the revision of the Asylum Law and the new law on foreigners.<p>
The defeat in September was the defeat of this so-called "left" and its policy of abdicating to the right.<br>
<b>C&eacute;cile Pasche, </b>Movement for Socialism, Lausanne, Switzerland<p>

