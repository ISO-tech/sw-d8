<font face="Times New Roman, Times, serif" size="4">"Years of racism and hatred directed at the Hmong" </font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Behind the Wisconsin shootings</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 3, 2004 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">BREWING RACIAL tensions reached the boiling point in late November when a Hmong-American shot and killed six white hunters in Wisconsin's North Woods.<p>
Chai Vang, a 36-year-old Hmong-American, was hunting on November 21 when he was approached by white hunters who told him that he was on private property. Vang told police that after he climbed down from the hunting platform, the men surrounded him and spewed racial epithets like "gook, chink and fucking Asian" at him.<p>
According to Vang's statement to police, he walked away from the platform. But 20 yards away, he turned and saw one of the men pointing a rifle at him. Vang opened fire, killing six of the men and wounding two others.<p>
His bail was set at $2.5 million, and he was scheduled to be charged November 30 with six counts of homicide and two counts of attempted homicide.<p>
Hmong community leaders quickly responded to the shooting with condolences for the family of the slain hunters. But this horrible shooting has brought to the surface a pattern of racism against the Hmong that dates back well before the incident.<p>
The Hmong are from Laos, where the Central Intelligence Agency recruited many to be part of an anti-Communist secret army during the Vietnam War. Vang himself came to the U.S. in 1980 and got training as a sharpshooter in the California National Guard. Today, he lives in St. Paul, Minn., which has the greatest urban concentration of Hmong in the U.S.<p>
While few Hmong live in the Wisconsin area where the shooting happened, several have reported incidents of racism during hunting trips in the area. Ying Vang, executive director of a Hmong community center, told the <i>New York Times </i>that whites regularly hurl racist insults at Hmong-American hunters.<p>
Curt Hubatch, a community activist from the Hayward area near the site of the shooting, told <i>Socialist Worker, </i>"I think the Mr. Vang's reaction was simply the result of years of racism, and hatred, directed towards him as a minority in this country. Probably within the past 10 years, there hasn't been a hunting season that has went by where I haven't heard a negative comment about Hmong hunters."<p>
Ilean Her, director of the Council on Asian-Pacific Minnesotans, says that she has now received several phone calls from Hmong school teachers whose students are scared--and others from Hmong hunters who want to speak out about the racism that they have experienced.<p>
"The level of violence was a surprise, but the run-in is not a surprise," she told <i>Socialist Worker.</i> "The Hmong hunters are scared, and they've also realized that it's time to speak out about what has happened to them."<p>

