<font face="Times New Roman, Times, serif" size="5"><b>SF hotels and union agree to end lockout</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Adrienne Johnstone</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 3, 2004 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--The 38-day lockout of San Francisco hotel workers ended November 23 when UNITE/HERE Local 2 and the Multi-Employer Group (MEG) agreed to a 60-day cooling-off period. Workers will be allowed back on the job, but prohibited from taking any action in the contract fight.<p>
The end of the lockout has been touted as a victory for the workers who have been on the picket line since an initial two-week strike in early September. Some good will come of the return to work--but in the context of the battle for a fair contract for Local 2, it cannot be seen as a step forward.<p>
The MEG would have liked to starve the workers into signing a five-year contract with monthly health insurance premiums of up to $300. Winning a two-year contract has been the key demand in this battle, which seeks to link up San Francisco with hotel workers in other major U.S. cities. <p>
After the state granted unemployment benefits and the employee health providers agreed to extend coverage into the New Year, it appeared that workers could stay on the picket lines longer than anticipated. Instead, the hotels chose to allow workers back without a contract rather than face the peak holiday shopping and travel season with picket lines at every hotel.<p>
Mayor Gavin Newsome, moreover, withheld city business from the hotels until they accepted his proposal for a cooling-off period.<p>
The hotels have agreed to withdraw their appeal of workers' unemployment benefits during the lockout. They've also agreed to a plan put forward by the union to cover workers' health benefits until a new contract is signed.<p>
However, the money for the benefits will come from a joint union-hotel fund--which implies that the union should be partly responsible for covering the benefits of workers locked out by the bosses. This temporary "solution" for providing health benefits exposes the weakness of the union strategy.<p>
Local 2 urged picket captains to comply with hotel requests not to block entrances and to keep noise down on the lines. Now the union is portraying the return to work as a victory.<p>
While it's true that Local 2 has not taken concessions in return for getting back to work, the lockout is not "beat" until a new and better contract is signed. The question now is whether union leaders will settle for less at the bargaining table--and if they do, whether the rank and file is willing to return to the picket line to fight to keep health benefits and win a two-year contract.<p>

