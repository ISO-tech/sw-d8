<font face="Times New Roman, Times, serif" size="4">County board votes to close King/Drew trauma center</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Gutting public health care in LA</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Yasser Giron and Sarah Knopp</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 3, 2004 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE HEALTH care crisis in Los Angeles took a further spiral downward when the County Board of Supervisors voted November 23 to close the second-busiest trauma center in LA's public health care system.<p>
The board voted 4-0 with one abstention to end trauma services at the Martin Luther King Jr./Drew Medical Center, which is located in South Los Angeles in one of the poorest--and most medically underserved--areas in the county. <p>
With this trauma center closed, critically injured people--such as gunshot victims and those injured in severe car accidents--will have to travel much farther to receive care. Many will miss the critical window of time--known to health care workers as the "golden hour"-- when doctors can save many victims of severe trauma. <p>
King/Drew Medical Center was built just south of Watts in the aftermath of the 1965 riots, after a government commission found that grossly inadequate medical care was a contributing factor to the rebellion. Today, the hospital serves an area described by the media as the "epicenter of violence" in this battle-torn city. <p>
Last year, the King/Drew trauma center treated more than 2,000 patients with life-threatening injuries, often from shooting and stabbings. The medical center's emergency room, where less serious injuries and illnesses are treated, sees an estimated 47,000 patients a year, and isn't slated to close, according to the Board of Supervisors.<p>
But residents recognize that the cost of closing the trauma center will be measured in more lives lost. They organized passionate protests--along with local political and religious leaders, as well as medical students assigned to King/Drew--to keep the trauma center open and demand more resources for the hospital. <p>
Community action culminated November 15 at an LA County Board of Supervisors meeting, where more than 3,000 people gathered to plead for the trauma center to be kept open. Among those organizing the protest were Service Employees International Union Local 660, Action for Grassroots Empowerment and Neighborhood Development (AGENDA) and Rep. Maxine Waters' (D-Calif.) office. Rev. Jesse Jackson, who attended the protest, called it the largest and most energetic community demonstration in South Central since the civil rights movement. <p>
To most people from the area, the reason for closing the trauma center seems obvious--to the wealthy of Los Angeles, the lives of people of color aren't considered valuable. <p>
County officials justified the shutdown by pointing to serious lapses in care at King/Drew that were identified by hospital inspectors. Thomas Garthwaite, the head of the county's health department, claimed that the trauma center put too much strain on the rest of the hospital's resources. "The stress of caring for the most complex patients has inhibited the ability of the hospital to overcome the significant challenges it faces in restructuring and improving the quality of care," he wrote in a letter. "The most cost effective way to achieve a safer clinical environment is to lower the number of critically ill patients entering the hospital."<p>
At the November 15 hearing, Jackson compared this logic to the Bush administration "saying in Iraq, 'I'm going to bomb you to save you.' It's the same with the hospital." Rep. Waters was also on hand to speak up for the trauma center, along with state assemblyperson Mervyn Dymally, who accused the county board of "an organized effort to dismantle this hospital."<p>
In fact, the county secretly negotiated a deal with a private downtown hospital, California Hospital Medical Center, to open a trauma center that would take many of those who would ordinarily go to King/Drew. The agreement represents another example of privatization and profit put before ordinary people's lives.<p>

