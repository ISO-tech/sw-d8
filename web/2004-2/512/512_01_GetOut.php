<P><font face="Times New Roman, Times, serif" size="4">Horror in Iraq</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Get out now!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 17, 2004 | Page 1</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ONE THOUSAND U.S. soldiers killed in Iraq. This grim milestone reached last week is further evidence that the Bush administration's brutal occupation of Iraq continues to unravel more quickly than ever.</P>
<P>As horrible as the number of U.S. dead is, it pales in comparison the number of Iraqis killed. According to the Associated Press, recent estimates suggest that more than 10,000 Iraqis are dead as a result of the war and occupation--just in Baghdad and nearby towns. Add in all of Iraq, that number is surely many times higher.</P>
<P>And that was <I>before</I> the escalating chaos and bloodshed that erupted last week. In Tall'Afar, in northern Iraq, U.S. forces killed at least 57 in an effort to win back control of the city from insurgents. In Falluja--a flash point of resistance since the occupation began--at least 15 people were killed and dozens more wounded when a U.S. rocket ploughed into a marketplace and an ambulance.</P>
<P>But the most shocking violence of all came in Baghdad, where at least 60 people were killed by U.S. forces on September 12 alone--as American troops battled supporters of radical cleric Moktada al-Sadr in Sadr City, a huge and desperately poor neighborhood. In the most horrific incident, a U.S. helicopter fired on a crowd of civilians that had gathered, cheering, around a disabled U.S. Bradley armored vehicle.</P>
<P>Incredibly, the U.S. at first tried to explain its firing on the crowd as a humanitarian gesture. "The helicopter fired on the Bradley to destroy it after it had been hit earlier and it was on fire," said Major Phil Smith of the 1st Cavalry Division. "It was for the safety of the people around it."</P>
<P>No one can ask Mazen al-Tumeizi if he feels "safe." The screams of the al-Arabiya journalist--"I am a journalist! I'm dying, I'm dying!"--were broadcast live after U.S. shrapnel sliced into his back, killing him. Across the country, at least 110 people were killed and hundreds more wounded in clashes on the same day.</P>
<P>The recent surge of resistance forced the Bush administration to finally admit that whole cities in Iraq--including Samarra, Ramadi, Baquba and Falluja--are "no-go" zones for both U.S. troops and the new Iraqi police forces. Other cities like Karbala and Najaf remain under U.S. and government control, but are prone to "instability."</P>
<P>Recent weeks have seen a shocking rise in attacks against U.S. forces--an average of 87 a day in August, more than double the average of 2003 and the first half of 2004. "We're dealing with a population that hovers between bare tolerance and outright hostility," a senior U.S. diplomat in Baghdad told <I>Newsweek. </I>magazine. "This idea of a functioning democracy here is crazy. We thought that there would be a reprieve after sovereignty, but all hell is breaking loose."</P>
<P>The Bush administration is desperate to make sure that Iraq remains on track for scheduled January elections. But this latest upheaval means that Iraqis in cities where the resistance is strongest may simply not be allowed to vote at all--showing that Washington's concern isn't "liberation" or "democracy," but keeping a puppet regime in place.</P>
<P>In the U.S., growing numbers of people oppose the occupation, but the Democrats are in full support--with John Kerry dismissing the mere suggestion of withdrawal as a "cut-and-run" strategy. Some Democrats are even trying to out-Republican the Republicans, demanding that the Pentagon take an even more aggressive approach in Iraq. "We need a strategy now," Rep. Ike Skelton (D-Mo.) told <I>Time </I>magazine, "to force the insurgents from their holes."</P>
<P>The antiwar movement needs a strategy, too. We need to send a message today to both parties: End the occupation and bring the troops home now!</P>


