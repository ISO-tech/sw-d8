<P><font face="Times New Roman, Times, serif" size="5"><b>Republicans and Fox News at work</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 17, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE REPUBLICANS and their bosom buddies at Fox News stooped to a new low in their efforts to fix the November election. A local Fox News reporter in Arizona claimed that a feminist student group at the University of Arizona might be committing an "unintentional felony" by registering out-of-state students to vote at a campus event.</P>
<P>A Republican county official backed up the allegation, telling Fox that if students are "only here to attend school, and their intention is to immediately return to where they came from when school is over, then they are not residents of the state of Arizona for voting purposes, and they cannot register to vote here."</P>
<P>Students who "lied" about their residency status would be committing a "felony offense." Wrong. Federal law guarantees the rights of students to register to vote in federal elections where they attend college.</P>
<P>The U.S. Supreme Court has specifically ruled that college communities have to allow students to register--and that the Federal Voting Rights Act prohibits states from imposing "durational residency requirements." The Arizona students refused to be intimidated--and formed a campus-wide coalition to challenge the Fox News fraud.</P>


