<P><font face="Times New Roman, Times, serif" size="4">Arrested during the RNC protests</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Locked up for the "crime" of protesting</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 17, 2004 | Page 4</font></P>

<P><font face="Times New Roman, Times, serif" size="3">RACHEL CLARKE-ALVAREZ was arrested during the protests against the Republican National Convention in New York, and spent 46 hours in detention. She wrote this article describing what happened.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>ON AUGUST 31, we all set out from Union Square at about 8:45 p.m. to protest the Republican National Convention in New York City. We clapped our hands and marched single file down 17th Street on the sidewalk, chanting, "We want peace." </P>
<P>There were about 75 of us as we reached a barricade and orange netting at the intersection of 17th and 5th Avenue. Police told us that they were working out a parade route for us and to please just wait, as the barricade was there to allow them to deal with another situation. </P>
<P>We realized, once police on motorcycles and foot arrived minutes later with their clubs drawn, that we were the "situation." Despite our repeated requests to leave and the fact that we were peaceful, the cops began grabbing us at different points in the line and cuffing us. </P>
<P>We were never told that we were under arrest, nor were we ever read our Miranda rights or informed of charges. From there, we went to Pier 57. They put us--all handcuffed--into a large co-ed pen where we were made to stand in long lines to be searched. </P>
<P>We were denied access to the bathroom and water for approximately three hours. I remained handcuffed for four hours. Over the next 16 hours, we were shuffled around from one overcrowded, dirty pen to the next. </P>
<P>Our requests to make a phone call, to see an attorney, or even so much as a plastic bag to sleep on, were denied. Some of us, myself included, opted to stay up all night, while most gave in to exhaustion and woke up the next day with chemical burns and rashes from having rested on a floor contaminated with oil and anti-freeze.  </P>
<P>I left Pier 57 to be transported on a bus with cages around each of the seats, which are typically used in transporting convicted persons to a correctional facility. When we arrived at the correctional cell, this was the first time we were asked about our medical conditions, and offered wipes to clean ourselves with. We had been detained for over 24 hours at that point. </P>
<P>Many ultimately refused medical attention, however, due to the police making it clear that they would need to go to the hospital, which would prolong detainment by between 10 and 20 hours. The authorities continued to tell us that we were going to see a judge. However, all we kept seeing was another overcrowded cage. At times, there were over 125 of us in a cell meant for 75. </P>
<P>I wasn't allowed to make a phone call for 26 hours, and of all of the cells I saw, only one had gym mats on the floor that allowed any of us to sleep. We learned about a court injunction that would begin fining the city of New York if we weren't released by 5 p.m., and we began to organize chanting and cheering to begin at 4 p.m. </P>
<P>As we remained in our last overcrowded cage and watched the clock tick on a wall across the hall, we screamed in unison, "Five o'clock...time to go!" I went before a judge at 6:15 p.m., 46 hours after I was detained.</P>
<P>Afterwards, I walked across the street to be welcomed by hundreds of supporters. Despite the horrible nature of the ordeal, I vowed at that moment to keep fighting and organizing against this system that looks more and more like an out of control Orwellian police state every day.</P>

