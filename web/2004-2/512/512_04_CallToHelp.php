<P><font face="Times New Roman, Times, serif" size="5"><b>A call to help an antiwar veteran</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 17, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I><BR>
Frank Cabadiana, my personal friend and companion in the battle of conscientious objector Camilo Mejia, is totally desperate, because his wife is about to be deported to Honduras. Cabadiana, a staff sergeant with 23 years of service in the Army, was a combatant in the war on Iraq from March to November 2003. </P>
<P>He was injured twice due to grenade explosions, that caused him almost total loss of hearing and wounds in both legs, which it make it nearly impossible to walk for long periods. He has already suffered two heart attacks and earned several medals for his heroism in battle. </P>
<P>He's married, has one 10-year-old son and resides in the state of Florida. He reports: "My wife Patricia Acosta Delgado was arrested by the Miami police in August for a traffic violation. The police called immigration agents because she lacks identity papers. </P>
<P>"She was taken before an immigration judge and was sentenced to be deported in mid-September. At present, she is detained at Monmouth County Correctional Facility in Freehold, N.J. </P>
<P>"When arrested, she was chained like a criminal. She is my only hope to live, and since I am disabled, she's the only one able to take care of our son. Please publish my story because I'm demanding justice and aid from the federal government since I consider that all the years I served for the U.S. are enough to prove I'm a real American, and I proved it with my performance in Iraq. </P>
<P>"My family deserves to stay together, and it's minimum what I'm requesting of the federal authorities: forgiveness and mercy for my wife. I have been calling many people and they do not return my calls. Please do justice to a veteran of war."<BR>
<B>Fernando Suarez del Solar, </B>San Diego</P>
