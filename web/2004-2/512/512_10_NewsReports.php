<P><font face="Times New Roman, Times, serif" size="5"><b>News and reports</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 17, 2004 | Page 10</font></P>

<P><font face="Times New Roman, Times, serif" size="4"><b>News from the Nader campaign</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Matthew Pillischer</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">PHILADELPHIA--Ralph Nader came to Philadelphia September 11 to speak to an enthusiastic, 400-strong crowd at the First Unitarian Church of Philadelphia for a campaign rally/fundraiser. In his speech, Nader called for the withdrawal  of U.S. troops from Iraq, adding, "That includes the oil companies who can't wait to get control over the third largest oil deposits in the world."</P>
<P>He continued: "Who's discussing this?  This issue of the Iraq war was taken off the table because John Kerry decided to say the words, 'Stay the course. Me too, George Bush.' </P>
<P>"Today is September 11th--the massacre three years ago," Nader said. "I just turned on the radio this morning and I heard someone reading every name of every person that was killed in that tragedy. Did you ever hear anyone even talk about the 18,000 Americans who die every year because they don't have health care, according to the Institute of Medicine? Did you ever hear anybody talk about the 58,000 Americans who die every year because of workplace-connected diseases and trauma? Did you ever hear anyone talk about the 65,000 Americans who die every year, according to the EPA, from air pollution diseases? Who weeps for these people?"</P>
<P><I>For more information, visit www.votenader.org.</I></P>

<P><font face="Times New Roman, Times, serif" size="4"><b>End the occupation of Iraq</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE RISING death toll in Iraq was a focal point for antiwar activists around the country. In New York, more than 200 people gathered on last-minute notice to express their grief and horror that the number of Americans killed in Iraq has passed 1,000. Significantly, the candlelight vigil, organized by United for Peace and Justice, was called to mourn all those who have died in Iraq and emphasized that the number of Iraqi deaths runs into the tens of thousands.</P>
<P>"It's time to set aside the political games that both Bush and Kerry are playing when we are talking about human life," said Aris Colonna from Veterans for Peace who was among those holding a banner that said, "Bring Them Home Now."</P>
<P>At the University of California-Berkeley, the Stop the War Coalition held a protest September 8 against Michelle Malkin, an ultraconservative Fox News commentator who defends the internment of Japanese-Americans during the Second World War--and calls for similar measures against people from Middle East today. During her speech before the College Republicans, a 150-strong, multiracial crowd of students protested loudly, chanting and yelling "Shame" for one-and-a-half hours while she spoke.</P>
<P>Across the bay in San Francisco, activists are organizing in support of Proposition N, which calls for an immediate withdrawal of U.S. troops from Iraq. What's unique about the proposition is it give citizens the chance to go on record in opposition to the war on Iraq and the continued U.S. occupation.</P>
<P>As Maria Guillen, a local representative of Service Employees International Union, explained to the San Francisco Chronicle, "[o]ur members get it, that it is the sons and daughters of the working class and the poor who are fighting and dying." </P>
<I><P>Meneejeh Moradian, Amanda Maystead and Sid Patel contributed to this report.</I> </P>

