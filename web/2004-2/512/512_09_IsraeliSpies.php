<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Behind the Israel spy revelations</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 17, 2004 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">BOMBSHELL REVELATIONS that high-level Pentagon analyst Larry Franklin passed classified information to Israel through the American Israel Political Action Committee (AIPAC) set off alarm bells in Jerusalem and Washington. AIPAC mounted a furious defense, lining up members of Congress on both sides of the aisle to vouch for its honor.</P>
<P>For some, the FBI investigation will prove beyond a shadow of a doubt that the Israel government and/or a domestic "Jewish lobby" controls U.S. Middle East policy. We should reject that characterization.</P>
<P>Israel annually receives more than $3 billion in U.S. aid. Egypt runs second at around $2 billion. Yet no one would seriously claim that the aid that Egypt receives is the result of an "Egyptian lobby." It's no coincidence that Israel and Egypt are the two top recipients of U.S. aid. Both are important allies in the region where the lion's share of the world's oil is located.</P>
<P>But the FBI investigation underscores what has been hidden in plain sight since the Bush administration took office. That is, that a network of neoconservative intellectuals and bureaucrats--many of them with ties to Israel's right-wing Likud Party--have occupied key positions in the Bush administration's national security apparatus.</P>
<P>Each of the brewing scandals that lurk behind the Bush administration's fa&ccedil;ade--like the outing of CIA agent Valerie Plame, Bush's use of forged documents to claim Iraq was trying to buy uranium, all of the claims surrounding Iraq's phantom "weapons of mass destruction" in the lead-up to the 2003 invasion, and revelations that neoconservative darling Ahmed Chalabi may be an Iranian double-agent--implicate one or more members of the neocon network.</P>
<P>The espionage allegations against AIPAC are just the latest chapter in this sordid tale. No matter how tempting it is to blame all of this treachery on a tight-knit cabal of neo-conservative intellectuals, the real culprits are the men to whom they report--President Bush, Vice President Cheney and Defense Secretary Rumsfeld.</P>
<P>The neocons wouldn't be the in the position they are if Bush, et al., hadn't been put them there. And they wouldn't be in the position to carry out these disastrous policies if Bush, et al., hadn't decided to shift U.S. Middle East policy.</P>
<P>In the early 1990s, the U.S., under both Bush Sr. and Clinton, wanted to push "the peace process" as its plan for "regional stability." Today's Bush administration has abandoned that strategy for the more nakedly imperialistic policy that neocons in both Israel and the U.S. have pushed for years.</P>
<P>If there seems to be no daylight between Bush's and Sharon's Middle East policies, it's because Bush and Sharon share the same outlook--not because Israel has been manipulating U.S. policy from behind the scenes.</P>
<P>It's very likely that Israeli secret services played some role in feeding bogus intelligence to the U.S. to help Bush make the case for war. But that's because the U.S. government wanted to find any pretext it could to invade Iraq--and the secret services of Israel, Britain and Italy were only too happy to oblige the Pentagon hawks.</P>
<P>This is not to say that Israel is blameless in the spy controversy. Of course Israel spies on the U.S. All allied countries spy on each other--as the U.S. showed two years ago when it spied on members of the United Nations Security Council.</P>
<P>But, Franklin was probably so used to working with Israeli agents and AIPAC on U.S.-government sanctioned projects that he may have considered sharing classified documents with them as nothing out of the ordinary. Franklin may be guiltier of collaborating with a foreign terrorist state than the dozens of the Arabs and Muslims who were caught up in the U.S. government's post-9/11 dragnet.</P>
<P>But will he face punishment as a spy? With so many interests vested in the U.S.-Israel relationship, don't be surprised if the FBI bombshell turns out to be a dud.</P>


