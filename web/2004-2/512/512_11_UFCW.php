<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>How can we rebuild the UFCW?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Darrin Hoop, UFCW Local 1105</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 17, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">UNITED FOOD and Commercial Workers (UFCW) Local 1105 President Sharon McCann summed up the recent contract ratified by 25,000 grocery workers in Washington's Puget Sound region by declaring, "This is the best agreement we could get short of a strike."</P>
<P>In fact, the Puget Sound deal is only the latest in a string of concessionary contracts the UFCW has negotiated this year. The two worst deals came after defeats in the four-and-a-half-month strike by Southern California grocery workers and the 11-month strike by Tyson workers in Jefferson, Wis.</P>
<P>In the face of these defeats, the union officials' slogan of "one day longer, one day stronger" has been rendered meaningless. The UFCW bureaucracy, moreover, with its bloated salaries, has once again reaffirmed its partnership with the corporate and political elite in the U.S.</P>
<P>We should all take tremendous inspiration from the Southern California grocery strikers who took on some of the richest and nastiest corporations in the U.S. But for our side to come out on top in the future, we have to chart a different course.</P>
<P>We should build a network that can link up rank and filers from Jefferson to Washington, D.C., and from Los Angeles to Seattle, with flyers and newsletters that can be used to help educate one another other--and to put forward plans of action.</P>
<P>In addition to the 25,000 Puget Sound grocery workers, contracts are currently being negotiated for 90,000 grocery workers in Denver, the Sacramento, Calif., area, and the San Francisco Bay Area. Members should demand that all these contracts get the same expiration date as the Puget Sound contract to begin the process of establishing a national master grocery contract.</P>
<P>We have to demand that our union begin its contract campaigns at least a year in advance. In addition, the union should have taken a strike vote and began building up a strike fund and a food bank by asking other unions, churches and community groups to donate money and food--and to commit to mobilizing to walk the picket lines.</P>
<P>Such an effort could draw tens of thousands of labor supporters out to back the grocery workers. But to achieve that, we have to aggressively take our issues to the public and help make people understand that our fight to win health care can set the standard for others to follow.</P>
<P>We also must be clear on the role of the state in labor disputes. When the courts pass injunctions limiting the size of picket lines--and when the police attack them--strikers need to be prepared for mass civil disobedience to keep the stores shut down. We also have to argue how labor's support for the Democrats sets back our struggle.</P>
<P>In the UFCW's Summer 2004 national newsletter, <I>Working America</I>, the entire 16 pages are devoted to getting John Kerry elected. A quote on the back cover argued, "neither the bargaining table or the picket line can solve the health crisis, but the voting booth can."</P>
<P>There wasn't a single word devoted to the tens of thousands of grocery workers who suffered big cuts in health care benefits in contract after contract this year. Plus, Democratic President Bill Clinton never delivered on his promise to deliver a national health insurance program.</P>
<P>Finally, we need to learn the lessons of the great strikes of the 1930s--that militant action is needed to turn back the employers' attacks. What's missing now are the networks of committed radical union members who are organized and armed politically to lead the struggles when the union officials refuse to lead. Rebuilding that core is our key task today.</P>


