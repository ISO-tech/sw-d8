<P><font face="Times New Roman, Times, serif" size="4">State party won't ratify "safe states" ticket</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Vermont Greens support Nader</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 17, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE VERMONT Green Party voted last weekend not to ratify the national party's presidential nominee David Cobb--and instead endorse Ralph Nader's independent campaign. That means that Cobb and running mate Patricia LaMarche will not appear on the Vermont ballot.</P>
<P>The state party's nominating convention endorsed Nader, but he won't be on the ballot as the Green candidate, either--at Nader's request. Instead, Nader is seeking to qualify as an independent.</P>
<P>The move by the Vermont Greens reflects deep dissatisfaction among members over the outcome of the party's national convention in Milwaukee in June. There, Cobb won the party's presidential nomination, but by a very narrow margin over supporters of Nader and his running mate Peter Camejo, a leading Green Party member.</P>
<P>Pro-Nader Greens say that Cobb--by exploiting a delegate structure that gave disproportionate weight to small states with weak Green Parties--was able to dominate the convention even though he won fewer than 15 percent of the votes cast where open primaries were held.</P>
<P>His candidacy represents a so-called "safe states" strategy, in which the Greens help John Kerry by not running an all-out campaign in "battleground states" where they could do well enough to tip the balance to George Bush. Cobb supporters often claim that he has renounced this strategy and represents an alternative to both the Democrats and Republicans.</P>
<P>But participants at the Vermont nominating convention said that LaMarche specifically acknowledged the campaign's "safe states" approach. "We could not in good faith recommend that our members support any candidate who would not be a strong alternative to the Republican and Democrat interests who control our government," Danny Weiss, a cofounder of the Vermont Green Party and member of the state party's steering committee, said in a statement to the press. "Many members felt that the Cobb and LaMarche ticket would in fact do more harm to the Vermont Green Party than good."</P>
<P>Several state Green Parties have taken up the question of whether to put Cobb's name on the ballot. In Utah, the Cobb-LaMarche ticket won't appear on the Green Party ballot line. And in California--the heart of the Green Party, where hundreds of thousands of members voted overwhelmingly for Camejo in the largest Green primary by far--the state coordinating committee narrowly voted down a proposal to convene a statewide special convention to consider replacing Cobb on the ballot.</P>
<P>National officials of the Green Party are bitterly opposed to these challenges, and Vermont Greens say they have been threatened with disaffiliation. James Leas, a Green Party candidate for state attorney general, said that as a delegate to the national party's coordinating committee, "I've seen quite a number of threats."</P>
<P>Leas said that LaMarche told the nominating convention that she personally "respected our right to make a decision as an independent statewide party." But, Leas added, she later said that "there may be consequences to your decision from the national Green Party."</P>
<P>The Nader campaign welcomed the Vermont party's endorsement, though it decided not to ask to be listed on the state party's ballot line. This was out of a fear that a lawsuit could disqualify Nader as the state Green Party's candidate after it was too late to qualify in any other way.</P>
<P>The Nader campaign is plenty familiar with lawsuits. Over the last week, said staffers, the campaign faced a total of 19 different suits in 16 states, all inspired by the Democratic Party's effort to keep the independent candidate off the November ballot.</P>
<P>The Republicans are up to their old tricks kicking people off the voter rolls, and the Democrats are trying their best to deny ballot status to a genuine alternative candidate. Nothing shows more clearly the corruption of the two-party system.</P>


