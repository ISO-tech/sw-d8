<P><font face="Times New Roman, Times, serif" size="4">Meager economic recovery leaves millions of people...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Desperate for a job</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 6, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WAGES ARE shrinking, newly created jobs are lousy, economic growth is slowing and the unemployment rate remains stuck--but profits and CEO pay are off the charts. That's the snapshot of the economic recovery provided by recently released economic statistics.</P>
<P>According to Stephen Roach, chief economist at the Wall Street giant Morgan Stanley, the more than 1 million new non-farm jobs created in the last four months "are little cause for celebration: the increases barely make a dent in the weakest hiring cycle in modern history." The industries responsible for a disproportionate share of new hiring are notorious for low wages: restaurants, building services and temporary employment agencies.</P>
<P>While the rapid loss of manufacturing jobs in recent years has slowed, there's little indication that the recovery will bring similar ones to replace them. The federal Bureau of Labor Statistics found that the number of part-time workers increased by half a million from March to June--a number nearly equal to the total growth in employment over the same period.</P>
<P>Overall, some 8.2 million people remain out of work--and the jobless rate remains at 5.6 percent, the same level as at the end of the recession in 2001. That's because when new jobs are created, people who had previously abandoned the job market start looking for work again. Thus, as companies begin placing want ads for new hires, they've been overwhelmed by the number of people looking for employment.</P>
<P>For example, DuPont's Washington Works in Washington, W. Va., expects thousands of applicants for 10 plant operator positions that pay $12 per hour, with a chance to reach $24 per hour over several years. Even low-wage, dead-end jobs in the nation's biggest cities are attracting large numbers of applicants.</P>
<P>"I got a drivers' job delivering flowers, making $8 per hour, and had to go through two interviews to get it," Dennis, who lives in Chicago, told <I>Socialist Worker. </I>Dennis said the supervisor who interviewed him had talked to 30 people for the job--and received applications from twice as many before the company stopped taking calls. "He said, 'All this for an $8-an-hour job--I guess that really says something about the economy,'" Dennis said.</P>
<P>The slack job market--along with an increase in inflation--has given employers leverage to drive down real wages to the same level as they were in the recession year of 2001. Profits, however, are strong--accounting for their highest share of the national income since the Second World War. In other words, workers are doing with less so that employers can have more.</P>
<P>That pressure on workers is set to continue. The government released figures showing that the U.S. economy overall grew at an annual rate of 3 percent from April to June, slower than expected and raising fears of another slowdown.</P>
<P>Corporate executives won't need to worry about their own pocketbooks, however. According to the research company Corporate Library, CEO compensation in 2003 shot up 22.2 percent in 2003 as top bosses cashed in their stock options as the financial markets improved.</P>
<P>The economic news led to claims and counterclaims by George W. Bush and John Kerry over the health of the economy. But both support the corporate agenda that's squeezing workers ever harder. </P>


