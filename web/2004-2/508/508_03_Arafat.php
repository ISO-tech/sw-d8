<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The crisis facing Yasser Arafat</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 6, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">TENSIONS WITHIN Yasser Arafat's Palestinian Authority (PA) are boiling over. Israel's relentless drive to grab Palestinian land, break the spirit of the Palestinian people, corner Arafat in his Ramallah compound and strangle Palestinian society economically and socially has ground down the PA's ability to formulate a coherent political response. This is the context of the explosion of popular anger among Palestinians at the hated PA security forces and rampant corruption within the PA.</P>
<P>In mid-July, the Jenin Martyrs Brigade--which grew out of the Popular Resistance Committees that united all major Palestinian factions at the beginning of the Second Intifada, or Palestinian uprising, that began in 2000--abducted Gaza Police Superintendent Ghazi Jabali, whose name often surfaces in stories about corruption among PA officials. The brigade refused to release Jabali until Arafat agreed to fire him.</P>
<P>Several hours of intense negotiations between the Jenin Martyrs Brigade and Fatah officials secured Jabali's release, but not before Jabali reportedly admitted to having embezzled $8 million in PA funds. The next day, Arafat did fire Jabali--and appointed in his place another loyalist, his nephew Mousa Arafat, to head the Palestinian Security Forces. But Palestinians despise Mousa Arafat as much as Jabali, so the crisis only worsened.</P>
<P>Hundreds of armed protesters took to the streets of Gaza, demanding the removal of Arafat's nephew and other militants thought to be aligned the Al-Aqsa Martyrs Brigades--part of Fatah's military wing. This forced Arafat to remove his relative, but public expressions of disgust at graft and corruption continue.</P>
<P>In early August, several thousand Palestinians demonstrated in the West Bank town of Nablus to show support for the Al-Aqsa Martyrs Brigades--the day after the brigades set fire to the offices of Jenin's PA governor and a Palestinian intelligence building for cooperating with Israel's security forces. </P>
<P>The other corruption scandal haunting PA officials involves huge amounts of cement imported by Palestinian businesses and sold to Israel for use in constructing the apartheid wall that snakes through Palestinian land. This scandal goes all the way to the top, tainting Palestinian Prime Minister Ahmed Qureia, whose family business turned a handsome profit on such deals.</P>
<P>In the end, the crisis facing the PA represents the bitter fruits of its bankrupt strategy of accepting the 1993 Oslo agreement as a vehicle for achieving a Palestinian state. Israel never planned to allow the creation of a viable Palestinian state and instead used the Oslo process to remove itself from the business of direct colonial rule, handing over the task of suppressing resistance to the PA's security forces.</P>
<P>As the Palestinian economy crumbled under the weight of Israel's siege--and in the absence of anything resembling a functioning state--the careful distribution of lucrative contracts and monopolies among Palestinian bigwigs became Arafat's primary method of securing political allegiance.</P>
<P>But the glaring inability of the PA to stop Israel's grab for more Palestinian land will continue to fan outrage at corruption--even as the Palestinian resistance can be expected to oppose any attempt by Israel and the U.S. to launch a direct assault on Arafat himself.</P>


