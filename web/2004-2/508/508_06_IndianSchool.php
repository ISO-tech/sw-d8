<P><font face="Times New Roman, Times, serif" size="4">India school fire that could have been prevented</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Horrors of privatization</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 6, 2004 | Page 6</font></P>

<P><B><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</I>,</B> <BR>
On July 16, 77 children were killed in a fire in Kumbakonam, in the south Indian state of Tamil Nadu, when the thatched roof of Sri Krishna private school caught on fire and quickly spread to the rest of the building. The roof of bamboo logs and coconut leaves collapsed, trapping the children inside. </P>
<P>There were marks on the walls that showed the children trying to tear through the bricks and concrete in their desperation to escape a fire that burned for over two hours. Since then, several more children have died from injuries, raising the death toll to 94.</P>
<P>Their deaths are horrifying, but what is even more outrageous is that these children are the latest victims of neoliberalism, privatization and profit.</P>
<P>In an attempt to unburden its own commitments, the Indian government has reduced spending on education to some 3 percent of gross domestic product. This has been the direct result of economic restructuring programs initiated by the Congress Party at the beginning of the 1990s, and then extended by the Bharatiya Janata Party.</P>
<P>The consequences have been that the public education system is so rundown in most places that parents are forced to send their children to a growing number of private schools that are unregulated, overcrowded and corrupt. Sri Krishna private school was one of these. </P>
<P>It used a thatch roof instead of a more expensive fireproof one; it had only one exit and only two narrow stairwells for a three-story building; it had no fire prevention or fire safety equipment; it was in a residential neighborhood with streets so narrow that firefighters could not directly approach the building (they had to employ cranes to get close enough!).</P>
<P>All of these things are technically illegal, but the laws are rarely enforced. Sri Krishna private school had been operating in the same condition for decades, and its license had been renewed as recently as January. In an attempt to deflect blame from themselves, local politicians have been blaming the teachers. </P>
<P>Others have proclaimed that they will enforce the law banning thatched roofs, but this is highly unlikely since it would require closing 75 percent of the state's elementary schools. This tragedy should serve as a constant reminder that there is high price to be paid for the free market.<BR>
<B>Snehal Shingavi, </B>Berkeley, Calif.</P>

