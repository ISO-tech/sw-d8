<P><font face="Times New Roman, Times, serif" size="4">Bush vs. Bush Lite</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Where's the choice?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 6, 2004 | Page 1</font></P>

<P><font face="Times New Roman, Times, serif" size="3">JUST HOW far will John Kerry go in pandering to Republicans? With its lineup of retired generals, displays of U.S. flags and endless talk of a "strong" America, the Democratic National Convention in Boston last month was explicitly aimed at conservative voters.</P>
<P>Kerry even bragged about supporting the Republicans' agenda of slashing government spending on programs for poor and working people. "When I came to the Senate, I broke with many in my own party to vote for a balanced budget, because I thought it was the right thing to do," Kerry declared.</P>
<P>No wonder Kerry--and the entire convention--left many progressive commentators cold. "I'm already getting tired of the bulk e-mail messages claiming that Kerry is the embodiment of progressive dreams," wrote columnist Norman Solomon. "Please."</P>
<P>Naomi Klein, the author and <I>Nation </I>columnist, wrote about "Kerry's vicious support for the apartheid wall in Israel, his gratuitous attacks on Hugo Ch&aacute;vez in Venezuela and his abysmal record on free trade." And in recent a speech, <I>Fahrenheit 9/11 </I>filmmaker Michael Moore warned that if Kerry is "weak-kneed and wimpy and wishy-washy," he "will encourage millions to stay home" on Election Day.</P>
<P>Nevertheless, Moore, Klein and Solomon all <I>support </I>John Kerry--in the name of stopping George W. Bush. They may dislike Kerry, but they claim that Bush is such a danger that he must be stopped at all costs.</P>
<P>Yet Kerry has made it plain that his presidency would embrace all the main elements of the Bush agenda. Kerry, like Bush, wants to continue the occupation of Iraq--and increase the already bloated military budget.</P>
<P>He has vowed to limit government spending--other than for national security and health care--at the rate of inflation. This would lock in the Bush administration's budget cuts--and prevent the funding of urgently needed programs to create jobs, alleviate poverty, rebuild public housing and schools. And Kerry's health care proposal would keep the system in the grip of profit-hungry pharmaceutical companies and health insurance giants--and leave tens of millions uninsured.</P>
<P>Kerry's campaign slogan is "we can do better"--and we can. Not with Kerry and his conservative running mate, John Edwards, but with a genuine alternative--Ralph Nader for president and Peter Camejo for vice president.</P>
<P>Where Kerry-Edwards want to reward their corporate backers, Nader-Camejo call for taxing big business to pay for social programs, like universal, national health care. Where Kerry-Edwards want to keep the grip of U.S. imperialism on Iraq, Nader-Camejo call for ending the occupation--and bringing the troops home.</P>
<P>We don't have to accept a choice between George W. Bush and Kerry's copycat policies. Nader and Camejo are giving a voice to the vast majority left out of this rotten political system--and their campaign deserves our support.</P>


