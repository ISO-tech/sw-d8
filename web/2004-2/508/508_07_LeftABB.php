<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="4">The Anybody But Bush left slanders Ralph Nader</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>How can they defend Kerry?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 6, 2004 | Page 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">NOW THAT delegates have emerged from the Democratic National Convention (DNC) "united" and "on message," the Anybody But Bush (ABB) left might want to pause for a moment of self-reflection--at its own culpability in selling Kerry as an antiwar candidate, despite all evidence to the contrary, while slandering the genuine antiwar candidacy of Ralph Nader and running mate Peter Camejo.</P>
<P>Radical filmmaker and onetime Nader supporter Michael Moore told a "Take Back America" rally outside the DNC on July 27, "One thing I do know about Kerry, he will not invade a country like George W. Bush did." Moore continued, "So my appeal to the Nader voters...is that we have a different job to do this year. And [the Nader campaign] is so misguided and so wrong and so uncool."</P>
<P>Just days earlier, as Nader himself was addressing an audience of 500 at nearby Harvard University, someone bellowed, "How can you sleep at night with the blood of the soldiers who died in Iraq on your hands?" Ironically, Nader had just made clear his own opposition to the war, arguing that Kerry is "for the war and wants to stay in Iraq."</P>
<P>In June, the Green Party's Medea Benjamin prefaced her rejection of the Nader-Camejo ticket by stating, "I totally support their antiwar, anti-corporate, pro-democracy message"--before warning that Nader and Camejo "might well help elect George Bush. This would be disastrous for our party, our country and our planet."</P>
<P>Kucinich supporters got a taste of the "Nader treatment" at the DNC, when they arrived at the convention to find out there would be no debate on the war, and their candidate would not even appear on the ballot. As Charlie Underwood--who cast Minnesota's lone dissenting vote for Kucinich--said, "[We were told] if we voted for Kucinich, it would be our fault if Bush won." He added, "They have their plan: Run like Bush Lite, then blame Nader when you lose."</P>
<P>The DNC was an entirely scripted convention--without even microphones for delegates to speak (their role having been reduced to holding up the correctly colored Kerry sign on cue as television cameras rolled). "We're not here for Kucinich! We're here to elect John Kerry," a party operative shrieked at those uninformed delegates who insisted on wearing Kucinich t-shirts.</P>
<P>Peace signs were forcibly removed upon entering the Fleet Center, and Medea Benjamin herself was forcibly removed after she held up an anti-occupation banner. A <I>Boston Globe</I> poll showed that an overwhelming 95 percent of delegates to the DNC oppose the war in Iraq, yet the Democrats unanimously adopted a pro-war 2004 platform that would have made Barry Goldwater proud. </P>
<P>The Democratic platform even puts Iran on notice--"a nuclear-armed Iran is an unacceptable risk to us and our allies"--while portending ominously, "With John Kerry as commander-in-chief, we will never wait for a green light from abroad when our safety is at stake." </P>
<P>Kerry's acceptance speech at the DNC (which managed to squeeze in a denunciation of communism, in a bow to the right-wing crusade of a bygone era) caused even the Republican-owned <I>Chicago Tribune's </I>editors to comment, "There were moments when you had to remind yourself that this was the Democratic nominee for president who was speaking--such as when Kerry promised a larger military force, tax cuts, public school accountability and a focus on family values."</P>
<P>On July 16, Kerry was even more explicit about his war plans, when <I>Wall Street Journal</I> reporters asked Kerry whether he expects to have U.S. troops out of Iraq by the end of his first term. He replied, "At the end of my first term, I would consider it a failure of my diplomacy if we haven't reduced the number significantly." (Translation: "Don't hold your breath.")</P>
<P>The ABB's pre-emptive support for Kerry amounted to a self-fulfilling prophecy--leaving the rest of us to wonder what might have happened if the entire left had united behind an actual antiwar candidate, instead of smearing Nader simply because he is outside the Democratic Party.</P>


