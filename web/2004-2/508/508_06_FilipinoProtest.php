<P><font face="Times New Roman, Times, serif" size="5"><b>Filipino protesters force troop withdrawal</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 6, 2004 | Page 6</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</I> <BR>
</B>In securing the release of Angelo dela Cruz, the Filipino truck driver who was taken hostage in Iraq early this month, newly--and many say fraudulently--re-elected Philippines President Gloria Macapagal Arroyo was also saving her own skin. Dela Cruz was released July 20 after the Philippines government, agreeing to his captors' demand, pulled its 51 troops out of Iraq a month ahead of schedule.</P>
<P>Arroyo's three-week-old government was already facing daily protests over election fraud when dela Cruz was abducted. She tried to disperse those demonstrations violently, ordering police to use water cannons, tear gas and truncheons against groupings as small as 20 people. But when dela Cruz's captors publicly threatened to behead him, the father of eight, as Arroyo herself would later admit, became "a Filipino Everyman, a symbol of the hardworking Filipino seeking hope and opportunity." </P>
<P>The protests swelled, threatening Arroyo's fragile government and saving dela Cruz's life. It's easy to see why Filipinos identified with dela Cruz. Most are either unemployed or underemployed--and subsist, at most, on a few dollars a day. Little wonder, then, that human labor is the country's largest export commodity. There are 8 million Filipinos abroad--one million of them in the Middle East. Most work in menial jobs under terrible conditions. </P>
<P>Middle East workers are particularly vulnerable. Like dela Cruz, the 4,000 Filipinos who now work in Iraq risk death on a daily basis. Together, overseas Filipino workers send an estimated $8-10 billion to the Philippines annually. This translates to 10 percent of the population responsible for 18 percent of the gross domestic product. </P>
<P>"The only logical explanation for this change in policy and [Arroyo's] willingness to risk disappointing her allies is to tame the protests," Teodoro Casino, a congressman and head of BAYAN (New Patriotic Alliance), said. Arroyo was America's strongest Southeast Asian ally in the "war on terror," allowing U.S. forces to conduct military operations and set up bases in the country in violation of the constitution--and, in return, receiving more than $100 million in military aid.</P>
<P>That aid is now at risk. Threatening "consequences," U.S. Ambassador Francis Ricciardone recently flew to Washington for consultations. So, far from following through with her newly announced "Filipino first" policy, it is likely that Arroyo's next step will be to make it up to her colonial master. Arroyo doesn't deserve credit for saving dela Cruz's life--the thousands of Filipino protesters do.<BR>
<B>Eduardo R.C. Capulong, </B>San Francisco</P>

