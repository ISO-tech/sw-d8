<P><font face="Times New Roman, Times, serif" size="4"><b>"Move it, you balloons"</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">HUNDREDS OF thousands of balloons were packed into the rafters of the Fleet Center to be dropped after John Kerry finished his acceptance speech. But apparently, the balloons didn't cooperate.</P>
<P>And the Democrats' convention producer, Don Mischer, nearly had a heart attack over it. Mischer's...uh...commands to the balloons to fall faster were caught on live TV by CNN.</P>
<P>"All right, go balloons, go balloons," he ranted. "We need more balloons. All balloons! All balloons! Keep going! Come on, guys, let's move it. Jesus! We need more balloons. I want all balloons to go, goddammit. </P>
<P>"Go confetti. Go confetti. More confetti. I want more balloons. What's happening to the balloons? We need more balloons. We need all of them coming down. Go balloons...balloons? What's happening, balloons? There's not enough coming down! </P>
<P>"All balloons, what the hell! There's nothing falling! What the fuck are you guys doing up there? We want more balloons coming down, more balloons. More balloons. More balloons!"</P>
<P>--<I>San Francisco Chronicle</I>, July 30, 2004</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Democrats throw Whoopi to the wolves</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE DEMOCRATS can't take a joke--even when it's aimed at the Republicans. Republican Party attack dogs went nuts after a performance by Whoopi Goldberg at a recent fundraising event for John Kerry in New York City. </P>
<P>Goldberg peppered her performance with four-letter words and sexual references--and was blasted by the Republican National Committee, which called the event a "hatefest" and denounced Kerry for associating himself with such material.</P>
<P>Did the Democrats rise to Whoopi's defense? Did they defend her right to free speech--since, after all, she helped rake in huge amounts of cash for the Kerry campaign? Of course not.</P>
<P>Rumor has it that Democratic leaders informed her that she was no longer welcome to attend the Democratic National Convention. And in the wake of the Whoopi scandal, comedian Margaret Cho was "uninvited" from the Human Rights Campaign's (HRC) Unity 2004 event being held near the Democratic National Convention. </P>
<P>Originally asked to headline the event, which was supposed to "celebrate GLBT strength and unity," HRC spokesperson Steven Fisher said her performance would have diverted the event's message away from the organizers' aim of helping to elect John Kerry president. "The one who wanted Margaret Cho the most was probably [Bush administration political adviser] Karl Rove," Fisher added. "The type of message of distraction she would provide is what Rove would like."</P>
<P>"I'm not surprised at the reversal in light of how the Kerry campaign distanced itself from Whoopi's routine in response to the unrelenting media hype and Republican criticism," said Cho's manager, Karen Taussig. "It's Whoopi's job as a comedian to say things that are sometimes shocking. I wish they could have backed her up. </P>
<P>"Dennis Miller can make gay jokes about Senators Kerry and Edwards at a recent Bush rally in Wisconsin to a complete absence of media scrutiny. No one demanded a tape of that event or alleged that his comments as a comedian might reflect poorly on Bush."</P>
<P>--<I>Washington Blade</I>, July 30, 2004</P>

