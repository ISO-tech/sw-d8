<P><font face="Times New Roman, Times, serif" size="5"><b>The U.S. incarceration boom</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 6, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">A RECORD number of Americans--6.9 million--are under the criminal justice system's control, according to a Department of Justice report released last month. About 2 million currently are behind bars in prisons and jails across the U.S.--and nearly 5 million more are on probation or parole.</P>
<P>Even though crime rates have remained stable for several years, the incarceration rate has continued to soar--mostly as a result of tougher sentencing laws passed during the last decade by Democrats and Republicans seeking to outdo one another in proving their "tough on crime" credentials. As a consequence, state and local governments across the U.S. are straining under the costs of housing a massive prison population--and cutting spending on programs like job training and education to balance their budgets.</P>
<P>The report on incarceration also documents the incredible racism of the criminal justice system. About 32 percent of Black men will spend time in a state or federal prison, and 41 percent of all parolees are Black. In the words of an American Bar Association report on the mushrooming U.S. prison population, "the costs of the American experiment in mass incarceration have been high."</P>


