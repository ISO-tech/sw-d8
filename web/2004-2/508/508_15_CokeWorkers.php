<P><font face="Times New Roman, Times, serif" size="5"><b>Coca-Cola workers strike for health care</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Justin Akers</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 6, 2004 | Page 15</font></P>

<P><font face="Times New Roman, Times, serif" size="3">SAN DIEGO--More than 300 workers from the International Brotherhood of Teamsters Local 683 went on strike July 20 at the Coca-Cola Bottling plant here after voting down the latest contract offer by a lopsided tally of 292-12. At stake is the company's plan to "restructure" the company's health-care plan in the mold of the Southern California grocery chains that slashed health-care benefits for their union workforce. </P>
<P>"We have people who have worked here for 25 years that will not be able to afford their medications," one worker told <I>Socialist Worker.</I> According to the latest offer, the company plans to shift health care plans from Kaiser to a Blue Cross/Blue Shield "in-house" plan in order to minimize its contributions to "skyrocketing" health care costs while maintaining coverage for its employees. </P>
<P>Modest wage increases will be dwarfed by increasing health payments over the life of the contract--an oily process that will allow the company to shift a larger percentage of the costs onto the workers. The plan will lead to a 500 percent increase in premiums and a 400 percent increase in co-payments, while the company will pocket millions per year, according to the union. </P>
<P>On top of this rollback, the company plans to introduce a two-tiered wage structure by erasing sick pay, funeral and jury leave for new employees. The company has stonewalled negotiations, displaying a resolve that workers attribute to the confidence to enforce new industry standards. </P>
<P>"It's clear that they have wanted this since they saw what happened with the grocery strike," said one worker who went on to point out that the productivity of the workers at the plant led to a 400 percent increase in profits in the last year. While the workers are facing cutbacks, the company's vice president was rewarded with a $24 million dollar retirement package. </P>
<P>Despite the threatening presence of police and company guards, the pickets have remained strong, and the company is having difficulty training scabs to operate the highly specialized bottling equipment. The average daily production, about 100,000 cases per day, has plummeted to less than 3,000. </P>
<P>Support from the community has been generous, with a steady stream of horns blaring in solidarity and the frequent delivery of food and water from individuals and local restaurants. While there has been minimal organized support from other San Diego unions, the local has launched a citywide boycott effort. </P>
<P>The success of this strike will determine the direction for upcoming contracts in other bottling plants around the country. As one worker put it, "We are fighting to save the right to health care for our families and all working people."</P>


