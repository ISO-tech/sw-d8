<P><font face="Times New Roman, Times, serif" size="4">The reality facing undocumented immigrants...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Daily life is a silent war"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 3, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
It shouldn't be too surprising that, in a society where basic human needs are measured on a grid of profitability, television producers inevitably find ways to profit off of the desperation of ordinary people. The latest and most repugnant example is the new reality show turning up on Spanish-language channels on the West Coast.</P>
<P>Titled <I>Gana la Verde </I>(<I>Winning the Green</I>), undocumented immigrants compete on television to win legal counsel to improve their chances of obtaining a green card. In <I>Fear Factor</I>-like fashion, contestants engage in risky stunts or repulsive acts to test their threshold for courage and tolerance.  These include eating burritos crammed with live worms, jumping off speeding trucks and scaling tall buildings to wash the windows.</P>
<P>Unlike the participants of other reality shows, these contestants are truly competing for their lives. For the majority of undocumented workers in this country, daily life is a silent war. They work in the most deadly occupations for the lowest wages, and they live with the daily risk of being apprehended and separated from your family.</P>
<P>But this is a war with great contradictions. There is a general consensus among policymakers that protecting access to cheap labor for the owning class trumps the actual stated policy of keeping undocumented people out of the U.S. This paradox produces the circumstances where over 8 million people are forced to live in a political and economic limbo.</P>
<P>While current border policy has led to the tragic deaths of over 3,000 people in the last decade, the bulk of current immigration policy is structured to maintain a legal and physical apparatus within the country in order to isolate the undocumented population from the rest of society. The Border Patrol's recent "roving raids" across Southern California are less an attempt at wholesale deportation and more a reminder of the threat of deportation. </P>
<P>As an increasing amount of immigrants join unions and advocate for their rights, "immigration control" rises in tandem--a subtle reminder of the extent of their "welcome" in the United States. This method of creating "parallel societies" determined by "legality" sustains a sub-class of workers without rights--quite a lucrative enterprise for those in the business of exploitation. And business is good.</P>
<P>Fake lawyers--calling themselves notarios--promise desperate people legal aid in obtaining residency and work permits, only to later abscond with their money as well as their hopes. Charlatan notarios find easy pickings among the growing ranks of a disempowered populace. As long as we ignore these "parallel societies" created in the interest of the few, so too do we remain victims.<BR>
<B>Justin Akers, </B>San Diego</P>

