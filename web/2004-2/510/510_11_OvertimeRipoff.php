<P><font face="Times New Roman, Times, serif" size="4">Why didn't organized labor mobilize in protest?</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Bush's overtime cuts kick in</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 3, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--Overtime for millions of workers in the U.S. vanished into thin air August 23, when the Bush administration's new rules on overtime went into effect. The new rules, which the administration has worked hard to push through for a year, drastically cut the number of jobs that are eligible for overtime pay.</P>
<P>Initially, the White House tried to sell the overtime rip-off as a needed adjustment to outdated rules in the 1938 Fair Labor Standards Act (FLSA), claiming that the new rules would make more workers eligible for overtime pay after 40 hours work. The new rules do raise the income cap for workers to be eligible for overtime from the antiquated figure of $155 a week (or $8,060 a year) to $455 a week ($23,660 a year).</P>
<P>But $23,660 is still an incredibly low amount, and, according to the Economic Policy Institute (EPI), the number of employees eligible will decrease over time because it isn't indexed for inflation. "An administration that cared about low-wage workers would have raised the threshold to at least keep pace with inflation since 1975, in other words, to at least $28,075," wrote the EPI's Ross Eisenbrey.</P>
<P>.But this is just part of the Bush administration's overtime disaster. According to the new rules, millions of workers' jobs could be reclassified, thus becoming ineligible for overtime. For instance, workers in the food industry who spend most of their time doing manual work but who sometimes instruct others could be reclassified as "team leaders" or "executives" and lose their overtime.</P>
<P>Also, special training could make a worker ineligible. So if a worker "attained advanced knowledge through a combination of intellectual instruction and work experience," they could lose their overtime rights. If an employee received training while they were in military service, for example, these new skills could cost them eligibility for overtime pay.</P>
<P>According to the EPI, as many as 6 million workers will lose overtime eligibility. Already, American workers work more hours than workers in just about any other industrialized country.</P>
<P>The average two-income family works 660 more hours--16 weeks--than they did in 1979 to make ends meet. Now the federal government wants them to work for free. Unfortunately, labor leaders' strategy to fight this attack has largely been to rely on pro-labor Democrats to block the legislation.</P>
<P>Hundreds of thousands of workers and supporters signed online petitions or wrote letters to the White House about overtime, but little effort was made to mobilize those workers on the streets in protest. We will have to begin organizing an active opposition to these attacks on workers--an opposition so loud the Republicans and Democrats can't ignore it.</P>


