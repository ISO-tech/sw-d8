<P><font face="Times New Roman, Times, serif" size="4">New statistics show poverty on the rise</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>No recovery for the poor</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 3, 2004 | Page 12</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE RANKS of the poor and medically uninsured are growing--and the most vulnerable are doing worst of all. According to the Census Bureau, 1.3 million fell beneath the official poverty line in 2003, despite the second year of economic recovery.</P>
<P>The poverty rate for children jumped from 16.7 percent in 2002 to 17.6 percent last year --that is, some 12.9 million kids and teenagers. Overall, the poverty rate rose from 12.1 percent to 12.5 percent--amounting to nearly 36 million people.</P>
<P>"We have had a generation with basically no progress against poverty," Sheldon Danziger, a professor at the University of Michigan, told the <I>New York Times. </I>"The economic growth is not trickling down to the poor." But even the official poverty statistics--appalling as they are in the world's richest country--understate the problem.</P>
<P>The government's poverty line for a family of two adults and two children is $18,660--a ludicrously low figure, especially in the nation's big cities. As the Economic Policy Institute pointed out, the decline in income has been concentrated in what are already the poorest households.</P>
<P>For example, poverty rates in female-headed households increased to 28 percent, up from 26.5 percent in 2002. The rise in poverty was the result of an overall decline in median household income, after accounting for inflation--a drop of 3.4 percent since the year 2000, to $43,318 today.</P>
<P>This decline in income is the result of the worst period for job creation since the 1930s. The slack job market has given employers leverage to keep pay low. Real weekly earnings have dropped 0.9 percent since the beginning of 2004.</P>
<P>Meanwhile, many people who are still working have seen their health insurance cut back or eliminated. The numbers of the uninsured rose last year by 1.4 million, bringing the total to 45 million people.</P>
<P>The percentage of people covered by employer-provided health insurance dropped to 60.4 percent, the lowest level since 1993. At the same time, health insurance premium costs rose by 13.9 percent in 2003--with employers passing on an increasing portion of the costs to workers.</P>
<P>All this should give plenty of ammunition to Democratic presidential candidate John Kerry in his race against George W. Bush. But Kerry, while taking swipes at Bush for the lousy economy, has no program to counteract this social crisis.</P>
<P>His health care plan would have the government spend billions to pick up the tab for some catastrophic medical costs--but would have little impact on bringing coverage to the uninsured. And Kerry has been virtually silent on programs needed to create jobs, fight poverty, build affordable housing or rebuild public schools.</P>
<P>As <I>New York Times </I>columnist Bob Herbert wrote in July, "Amid all the muscle-flexing [at the Democratic National Convention] in Boston this week ('my homeland security platform is bigger than yours'), it was impossible to hear more than the merest hint or offhand whisper about the demoralizing decline in the fortunes of America's cities over the past few years...</P>
<P>"Tax cuts for the wealthy and the administration's hard-right ideology have removed much of the social safety net that we managed to weave over the past several decades, leaving us with a swelling population of vulnerable men, women and children. This has had a disproportionate impact on cities, and the outlook, both short- and long-term, is bleak at best. These are important issues that could be wrestled with if cities were on anybody's agenda. But they're not."</P>
<P>The issues of poverty and social decline are at the center of the Ralph Nader-Peter Camejo independent campaign for the White House. But the Democrats have pulled out all the stops to keep them and their proposals from being part of a public debate. The poverty and medical insurance statistics are only the latest example of why we need a real political alternative.</P>


