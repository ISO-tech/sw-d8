<P><font face="Times New Roman, Times, serif" size="4"><b>Free Majid!</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Steve Leigh</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEATTLE--Chanting "Majid needs his rights back. Let's repeal the Patriot Act!" more than 30 supporters gathered on August 27 to stop the deportation of Majid al-Massari. Organized by his coworkers at the University of Washington (UW) School of Nursing, people attended Majid's hearing and picketed outside to draw attention to his case.</P>
<P>Majid is a Saudi citizen and University of Washington (UW) employee who has asked for asylum since 1997. His father is a well-known Saudi dissident in exile in London. His brother has been brutalized by Saudi authorities.</P>
<P>Majid is in fear for his safety if he is deported to Saudi Arabia. Majid's asylum request has been stonewalled for seven years--and now the government wants to deport him for a supposed misdemeanor drug conviction in 2003 for which he served just three or four days in jail.</P>
<P>Even though it is being called an immigration case, the Department of Homeland Security is holding Majid under "special provisions" and treating him like a terrorist. Majid had been held in solitary confinement since his arrest on July 17, denied a shower for six days and isn't even allowed to have a copy of the Koran.</P>
<P>His lawyer, Cheryl Nance, found out about his arrest six days after it happened--from a story in the <I>Washington Post. </I>His coworkers have been terrorized as well. Federal agents seized Majid's computers and issued a gag order. Coworkers were not allowed to say that Majid had been taken into custody.</P>
<P>After the preliminary hearing--with supporters inside and pickets and press outside--Majid's potential deportation was delayed. Majid is no longer in solitary confinement.</P>
<P>At a new hearing on October 12, the judge will decide if Majid can be deported immediately as the government wants. Supporters promised to build support for Majid and come back October12.</P>
<P>This organizing by Majid's coworkers at the School of Nursing is a fine example of how people should respond to every repressive attack by the "war on terrorism." From their Web site, http://www.gardenfrog.net/majid/history.htm to the picket, his coworkers understand that "An injury to one is an injury to all!"</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>News from the Nader campaign</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Leighton Christiansen</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">IOWA CITY, Iowa--Independent presidential candidate Ralph Nader secured a spot on the Iowa ballot on August 26. Despite a last-minute challenge, a three-person election panel ruled unanimously that Nader supporters had gathered more than the necessary 1,500 signatures representing eligible voters in 10 counties.</P>
<P>Nader gave a vigorous "us vs. them" speech to more than 200 at Drake University on the eve of the election board's decision. Nader pointed out the hypocrisy of Washington's "war on terror."</P>
<P>While the Bush administration has spent billions on war, 45 million Americans have no health coverage. "No wonder 67 percent of the American people in a recent poll concluded that George W. Bush cares more about the interests of corporations than about the interests of people like them," Nader said.</P>
<P>Iowa is considered a swing state in the November election, with the polls very tight. A July <I>Des Moines Register </I>poll showed 46 percent for Bush, 45 percent for John Kerry and 2 percent for Nader, with a margin of error 3.9 percent.</P>
<P>While Nader's support in Iowa may be small, he is campaigning hard to improve his standing. Nader talked about an ambitious plan for whistle-stop campaign in the Midwest, home to most of the so-called swing states.</P>
<P>Nader campaign volunteers received plenty of abuse during the ballot signature drive, with people screaming that Nader would cost Kerry the election and stick us with Bush. Nader took on that argument in his speech. Nader attacked Kerry--the supposed "friend of labor"--for not even mentioning workplace deaths and for the similarities of his positions to those of Bush.</P>
<P>"Now, if you were running on a Democratic ticket, wouldn't you jump on those issues?" Nader asked. "But not if you're dialing for the same corporate dollars--from the coal industry, the oil industry, the drug industry and the hospital industry."</P>

