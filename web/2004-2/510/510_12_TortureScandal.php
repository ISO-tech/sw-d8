<P><font face="Times New Roman, Times, serif" size="4">Tortured at Abu Ghraib:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The scandal they can't hide</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 3, 2004 | Page 12</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE BUSH administration claims that the torture carried out in Iraq's Abu Ghraib prison was the work of a "few bad apples." But a pair of reports released last week--one by an outside panel, and one by Army generals--shows that the scandal goes much deeper.</P>
<P>Former Defense Secretary James Schlesinger, who led the outside panel, told reporters that approximately 300 cases of abuse are being investigated--at U.S. detention facilities in Afghanistan and Guant&aacute;namo Bay, Cuba, in addition to Abu Ghraib. "So the abuses were not limited to a few individuals," Schlesinger said. </P>
<P>The panels found that the sickening abuse of detainees at Abu Ghraib and elsewhere included acts of sodomy, beatings, nudity and lengthy isolation. In one horrific "game" played by military police (MP) at Abu Ghraib, dogs were used to terrify prisoners as young as 15 years old into urinating and defecating in fear. </P>
<P>The Bush administration has blamed the torture scandal all along on a handful of "immoral" soldiers. But both reports pin plenty of responsibility on military intelligence (MI) officials.</P>
<P>The Army generals' investigation concluded that in at least 16 cases--more than a third of the 44 cases the generals documented--"abuse by the MP soldiers was, or was alleged to have been, requested, encouraged, condoned or solicited by MI personnel."</P>
<P>The report also found that the interrogation tactics of military intelligence officers set the stage for the further escalation of abuse and torture. Staff Sgt. Ivan Frederick--one of those charged with torture--recently described how the abuse was encouraged by higher-ups.</P>
<P>"On the one hand, I was full of rage that this prisoner had injured a soldier," Frederick told the German magazine <I>Der Spiegel</I>. "And they'd told me, 'humiliate them.'" Frederick said he was encouraged by intelligence officers to break down prisoners for interrogation, by any means. </P>
<P>"It was about concrete results, and they weren't interested how they were achieved," he said. "There are definitely more people responsible for what occurred in Abu Ghraib, and many of them have not been charged." Some of those include CIA personnel.</P>
<P>According to the Army generals' report, CIA agents insisted that at least eight of their detainees be kept out of prison records--turning them into "ghost detainees" hidden from human rights groups, a violation of international law. At least one died.</P>
<P>All of this points to a pattern of abuse and torture that goes far beyond just a "small group" of sadistic soldiers. "The abuses were not just the failure of some individuals to follow known standards, and they are more than the failure of a few leaders to enforce proper discipline," concludes one report. "There is both institutional and personal responsibility at higher levels."</P>
<P>For example, the use of painful "stress" positions, nudity and dogs were not approved interrogation tactics--yet one report says that Gen. Ricardo Sanchez, the former senior commander in Iraq, approved such techniques.</P>
<P>Despite this, both panels refused to fault the Bush administration or military higher-ups. Instead, the reports say that a lack of "planning" and breakdowns in leadership fostered the environment in which the abuse occurred. </P>
<P>Schlesinger's investigation says that Pentagon officials--including Defense Secretary Donald Rumsfeld and the Joint Chiefs of Staff--failed to exercise proper "oversight" over detention policies, which "contributed" to an environment in which prisoners were abused. One panel recommends more and better-trained military police and intelligence specialists--and that all prisoners be treated in "a way consistent with U.S. jurisprudence and military doctrine, and with U.S. interpretation of the Geneva Conventions."</P>
<P>This isn't justice. After all, the Bush administration has already declared that "U.S. interpretation" of the Geneva Conventions includes "stress and duress" in interrogation techniques. Real justice for the detainees at Abu Ghraib won't come until George Bush, Donald Rumsfeld and the rest of Washington's war makers are held accountable for their crimes.</P>


