<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A defeat for the U.S. in Najaf?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 3, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">FEARING A wider revolt if it launched an all-out assault, the U.S. agreed to major concessions to halt bloody fighting in the southern Iraqi city of Najaf. The three-week battle between U.S. forces and the Mahdi Army of Shiite cleric Moktada al-Sadr ended last week after Washington was forced to fly in Grand Ayatollah Ali Sistani--who had been in London recovering from heart surgery--to broker a cease-fire.</P>
<P>The agreement reveals the depth of the crisis facing the U.S. occupation. It calls for the U.S. military to stay out of the city--which contains some of Shiite Islam's holiest sites--and specifies that Washington will foot the bill for damage to Najaf.</P>
<P>U.S. media accounts of the fighting portrayed Washington's assault on Sadr and his fighters as a battle the U.S. had to win. Yet in the end, Sadr--not the U.S. or its puppet government led by Prime Minister Iyad Allawi--came out the winner. As the <I>Los Angeles Times</I> wrote, "Under the deal, Sadr left the mosque with amnesty for any crimes he might have committed, an invitation to join in national politics and freedom for his militiamen, many of whom remained heavily armed."</P>
<P>By daring to stand up to the U.S., Sadr galvanized support among Iraq's Shiite majority. The fact that the U.S. had to negotiate a settlement rather than carry out an assault that would do even more damage to the Imam Ali Mosque, where Sadr's followers had taken refuge--shows that Washington realizes that outrage at the occupation is threatening to boil over. </P>
<P>Far from "disarming" militia fighters who could potentially threaten Allawi's government, Sadr ended the standoff with his forces largely intact. In fact, he is now free to rally support in other Shiite strongholds--like Baghdad's Sadr City, an enormous poverty-stricken slum.</P>
<P>"For Moktada, it is a wash," Juan Cole, a professor of Middle East history and expert on the Iraqi resistance, commented on his Web site. "He did not have Najaf until April anyway, and can easily survive not having it. His movement in the slums of the southern cities is intact, even if its paramilitary has been weakened."</P>
<P>Furthermore, according to the <I>Washington Post</I>, "U.S. military strategy has also suffered a blow, particularly since Najaf is the third confrontation in five months in which Iraqi insurgents fought American troops until they began to take losses, then agreed to a cease-fire so their fighters could rest and regroup."</P>
<P>As analyst Anthony Cordesman of the Center for Strategic and International Studies put it, "The U.S. has rolled the rock up the edge of a decisive military engagement, only to see it roll back down the mountain. Sadr will be perceived by many Iraqis as the victor, and Sistani as the man who had to rush to deal with Sadr in the face of a weak Iraqi interim government, whose leaders threatened and blustered and then could not act."</P>
<P>The increasing problem for the U.S. is that Najaf is one of several "hot spots" threatening to boil over. While U.S. commanders were worried about Sadr in Najaf, U.S. warplanes bombed Falluja, where the Marines had to retreat last spring. </P>
<P>The U.S. has all but withdrawn from the Sunni city of Samarra, north of Baghdad, and fighting is continuing in the town of Baqubah. There will be no peace or justice for ordinary Iraqis until the U.S. gets out.</P>


