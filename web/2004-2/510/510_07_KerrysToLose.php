<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why isn't Kerry trouncing Bush?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 3, 2004 | Page 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">OFFICIALS IN an administration that has gotten the U.S. bogged down in an unpopular war and overseen a net loss of jobs and a net increase in poverty should be polishing their resumes for their inevitable defeat in November. Yet according to the opinion polls, the November presidential vote remains "too close to call,"--with George W. Bush running about even with the Democrats' John-John ticket of Sen. John Kerry and John Edwards.</P>
<P>It is nothing short of incredible that Kerry is not already the odds-on favorite to win in November. Liberals and Democrats worry about a Bush "October surprise" (such as the capture of Osama bin Laden) or rigged voting machines, which will allow Bush to eke out a victory. But the real reason for Kerry's failure to bury Bush is more fundamental than any last-minute hijinks.</P>
<P>It lies in Kerry's and the Democrats' complete inability--and, increasingly, it seems, unwillingness--to offer anything positive to inspire support. On one question after another, Kerry has reiterated the standard "centrist," Republican Lite positions that have become standard for Democratic candidates. </P>
<P>And on the most crucial question of the day--the occupation of Iraq--Kerry has hewed about as closely to Bush's failed policy as he can. As a result, Bush's overall loss of support has not been reflected in any substantial shift towards Kerry.</P>
<P>The problem with the Bush Lite strategy is that it's a proven loser. Following this script, Al Gore turned what should have been landslide in 2000 into an election that was close enough for Bush to steal. </P>
<P>Democrats hope that the loathing of Bush among their supporters is so deep that it won't matter if Kerry gives them no other reason to vote for him than that he isn't Bush. They may be right. But this strategy runs the risk of making the presidential campaign so uninspiring as to fail to motivate Kerry voters to go to the polls in the so-called "most important election of our lifetime." </P>
<P>If 2004 produces another 2000-like deadlock, odds are that Bush will hold on to the White House. Although he won't say so, Kerry only became a viable candidate against Bush because Bush's war policy has turned into a disaster. But because Kerry has refused to distinguish himself from Bush on the war (as well as on a number of other issues), he hasn't really been able to pull into a substantial lead.</P>
<P>If Kerry promises no change from the policies that are driving people to want to fire Bush, Bush can quite reasonably ask the electorate why it should want to "change horses in midstream." Bush can ask: Do you want the real thing or the copy? The Republicans know that given this choice, voters usually go for the real thing.</P>
<P>This was the entire rationale for Bush's public challenge to Kerry to say whether he would still have voted to give authority for the invasion of Iraq "knowing what we know now." And Kerry gave Bush exactly what he was hoping for. In reaffirming his 2002 vote for the war, Kerry threw away any credibility he might have in challenging what should be Bush's biggest vulnerability.</P>
<P>In fact, in the wake of the warmongering Democratic convention, a Gallup opinion poll noted a slight up-tick in the numbers of those saying the war in Iraq was "worth it." Support for the war cracked 50 percent for the first time since June--when, during the high point of revelations about the U.S. torture of prisoners, 54 percent of those surveyed said the war was "not worth it." At the same time, Gallup found that Kerry and Bush were tied 47-47 among registered voters, a result that represented a shift towards Bush.</P>
<P>So if these figures are accurate, Kerry's pro-war stance hasn't improved his chances. It's actually helped rehabilitate Bush's disaster. For the people who run the Democratic Party, that's alright. As they showed in 2000 when they abandoned any fight against Bush's Florida election fraud, they would rather lose an election than upset the ruling-class consensus.</P>


