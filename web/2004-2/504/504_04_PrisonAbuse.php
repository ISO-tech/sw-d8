<P><font face="Times New Roman, Times, serif" size="4">Two million behind bars in the "land of the free"</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Prison abuses in the U.S.</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 25, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</I>,</B> <BR>
I, like so many other people of conscience, am incredibly incensed regarding the recent reporting of the abuses that have been occurring in the prisons of Iraq. The people of Iraq are still oppressed, but the oppressor has changed from the tyrannical Saddam Hussein to the imperialist United States.</P>
<P>Incensed as I am at the abuses suffered at the hands of our "ambassadors of liberation," I am not at all surprised. Prison abuse is not an anomaly. It occurs regularly in the United States. </P>
<P>When a prison sentence is placed on a defendant, it is a sentence to be confined for a specific amount of time. It does not include the regular physical abuse, rapes and systematic dehumanization that occurs in the environment of terror that our prisons have become. According to Human Rights Watch, inmates are beaten and sexually assaulted, both by other inmates and by guards; inmate-on-inmate physical and sexual violence is often allowed--facilitated even--by corrections officers. </P>
<P>Is this how society "rehabilitates" people? The privatization of prisons should also be an issue--correction officers buying stock in the prison, their profits based on the warm bodies filling their corporate quarters. </P>
<P>Would it not be in the best interest of a profiteer to keep an inmate for an indefinite period? How often are sentences extended based on profit principles? An appeasing picture is often painted in the media of the generic inmate: violent and deserved of their caging. </P>
<P>In 1980, about half of those in prison were convicted of violent crimes. In 1995, less than one-third of persons sentenced to prison were violent offenders. The United States has the highest imprisonment rate in the world. The land of the free cages over 2 million of its inhabitants. Here in California, the state has built 23 prisons in the past 20 years, and <I>one</I> university. Something is terribly wrong with this picture.</P>
<P>When society supports laws like "three-strikes" and a budget that prioritizes prisons above schools and incarceration before education, that society is headed for the ultimate collision--the ultimate domestic blowback.  It is time for a new system of rehabilitation, a new prioritization of both education and human rights--and for people to start making their voices heard.<BR>
<B>Melissa D. Burns, </B>from the Internet</P>


