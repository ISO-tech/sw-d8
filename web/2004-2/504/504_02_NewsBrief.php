<P><font face="Times New Roman, Times, serif" size="4"><b>Supreme Court helps HMO bosses</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">JUSTICE REMAINS blind when it comes to ordinary people screwed over by the HMO giants. In a unanimous vote, the U.S. Supreme Court ruled that patients who believe that their HMO refused to authorize necessary medical care have no right to sue in state courts.</P>
<P>The decision is a huge victory for the HMOs, which feared that they would be hit with substantial penalties and judgements in state courts--and a disaster for the 140 million people who covered by health insurance through employer-plan HMOs.</P>
<P>The justices "effectively slammed shut the courthouse doors to the thousands of Americans each year who suffer serious harm because their health insurers wrongfully and unreasonably blocked the patients' access to much-needed medical care," said Syracuse University law professor Peter Bell.</P>
<P>The decision focused on a provision of a federal law, the Employee Retirement Income Security Act (ERISA), that bars lawsuits against HMOs for not approving treatment ordered by a doctor--something that health care advocates have realized was a way for politicians to give the HMOs a huge handout.</P>
<P>The Supreme Court's unanimous opinion--written by none other than notorious dimwit Justice Clarence Thomas--insisted that the provision pre-empts state laws in Texas and nine other states that give residents the right to sue HMOs. The ERISA provision, according to Thomas, was perfectly fair to maintain "efficiency" in managed health care.</P>

