<p><a name="Fresh"></a><font face="Times New Roman, Times, serif" size="4"><b>When frozen food is "fresh"</b></font> </P>
<P><font face="Times New Roman, Times, serif" size="3">IT MUST have been a tribute to Ronald Reagan. Within days of the death of Reagan--who famously insisted that ketchup should be classified by the federal government as a vegetable--a federal judge tweaked the definitions for another junk food staple. U.S. District Judge Richard Schell endorsed the U.S. Department of Agriculture's changes in federal regulations that will redefine batter-coated French fries as a fresh vegetable.</P>
<P>Behind the scam is the Frozen Potato Products Institute, "which has spent decades pushing for revisions" to federal laws, according to the <I>Chicago Tribune. </I>Reading the scientific description of frozen fries pretty clearly undermines the potato baron's claims. "After partial dehydration, the potato strips are coated with an aqueous slurry," reads French-fry maker Lamb Weston's patent on how to make batter-coated fries.</P>
<P>"The aqueous starch enrobing slurry...is comprised of a combination of chemically modified ungelatinized potato starch, chemically modified ungelatinized cornstarch, rice flour and other optional ingredients." Not very fresh sounding. Then again, the USDA rule changes are so broad that chocolate-covered cherries, packed in a candy box, could qualify as...a fresh fruit.</P>


