<p><a name="Reality"></a><font face="Times New Roman, Times, serif" size="4"><b>The real unemployment rate</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">OFFICIALS IN Lawrence County, Ohio, had to wonder when their official unemployment rate dropped to 5.7 percent in April. There just seemed to be more people out of work than that. So the county decided to conduct its own survey--which estimated that the real jobless rate was 17.9 percent, more than three times higher.</P>
<P>"We didn't dream it would come out this high," said Buddy Martin, director of the county's Department of Job and Family Services. Why the difference? The county study found that 10.4 percent of those questioned didn't have jobs, and weren't looking for work. These so-called "discouraged workers" aren't counted in federal unemployment statistics.</P>
<P>If you look at the fine print, the federal figures show the same reality--that overall participation in the labor force continued to fall through the beginning of this year, even though overall statistics showed the economy in a recovery. The local study also found that one in five families have an income below the poverty level, and one in four households receive some type of government assistance, such as worker's compensation, Social Security or disability.</P>

