<P><font face="Times New Roman, Times, serif" size="4"><b>What the Reagan tributes left out</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Thank you, thank you, thank you for a beautiful article on the <I>real</I> Reagan. ("Good riddance to a bigot," <I>SW</I>, June 11) I'll never forget one story about him. He made a lot of "political hay" by calling the sports heroes of the moment, and congratulating them and inviting them to the White House. </P>
<P>The year the "local" team, the Washington Redskins, won the Super Bowl, a Black quarterback, Doug Williams, was the MVP. Williams waited in the locker room a long time for a phone call that never came. Reagan, the bigot, could <I>never</I> call a Black man and pat him on the back for a job well done!<BR>
<B>J.H., </B>from the Internet</P>

