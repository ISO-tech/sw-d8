<p><a name="Cintas"></a><font face="Times New Roman, Times, serif" size="4"><b>Dirty laundry at Cintas</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I used to work for Cintas in Kelowna, British Columbia, Canada, but recently I had to quit because of too many employees on my afternoon shift quitting--which resulted in me working 11- to 12-hour shifts and getting around three to four hours of sleep before having to drive my wife to her work at 5:30 a.m. </P>
<P>I started working at Cintas on May 6, 2003 as an unloader for $9 an hour. As the first few months passed, I saw someone quitting almost every week, and most of the employees weren't happy. In a short time span of two months, around 10 people quit leaving our afternoon understaffed.</P>
<P>I was quickly burning out, and I told my manager about this. He said, "You can deal with it," or something like that. All the lead hands were very young white males--late teens to early twenties. They definitely didn't have any plant experience. The older plant workers, women and minorities who applied for the lead positions never got them. </P>
<P>All the truck drivers are white males--no women or minorities. Whenever a new position opens up, it's because someone quit. They always hire from the outside, bypassing all those who applied from within the plant. In many cases, including mine, our medical and dental plan wasn't properly activated after our six-month probation period, and it took many months later to get it properly activated. </P>
<P>Management selects a few plant workers they really favor, and everyone else is almost ignored. Management just says, "Get it done," "Suck it up," and "It's not the power of the man, it's man power." </P>
<P>On occasion, management comes onto the floor with a clipboard and asks us their many "core values" and slogans. We have to answer them right if we want to advance and be favored. This is just the tip of what I know goes around at this plant.<BR>
<B>Former Employee at Cintas,</B> Kelowna, British Columbia, Canada</P>

