<p><a name="Nader"></a><font face="Times New Roman, Times, serif" size="4"><b>Reform support doesn't hurt Nader</b></font></P>

<P><B><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Your article on Ralph Nader ("Is Nader offering a left alternative?" <I>SW,</I> May 28) does him a huge disservice. The fact is that the Reform, Green, Socialist, Communist, and <I>many</I> other parties agree with Nader on many points, probably the most important of which is the need for a thriving plural democracy--which would be a major goal of a Nader presidency.  </P>
<P>Do Nader and these parties see eye to eye on all issues? Of course not. The fact of the endorsement of such a "leftist" from the extreme "right" speaks volumes not only about Nader's character, but also about his positions. By making such an endorsement, the Reform Party is essentially saying, "We know what actions you may take on the positions on which we differ, but we are willing to sacrifice them for the greater good your presidency would do to us, our country and the world." </P>
<P>This was a courageous act by the Reform Party, an act which shatters the tired illusion of "us vs. them," "left vs. right," "good vs. evil"--and I applaud them for it. Note that Nader hasn't come out and endorsed the Reform Party. They are endorsing him, and he is accepting it.  </P>
<P>Should Nader shun the Reform Party nomination? No! To "shun" such an endorsement would be nothing less than an act of blind hatred, a form of bitter divisiveness that dehumanizes the <I>people</I> of the Reform Party, who have the same fundamental human needs as the rest of our species--for freedom, security, prosperity and all the rest.</P>
<P>Shame on you for stooping to the level of the same old tired rhetoric of "good vs. evil," "left vs. right," "us vs. them" that has undermined the entire revolutionary struggle since its birth.<BR>
<B>Ryan Anthony Charlesworth,</B> from the Internet</P>

