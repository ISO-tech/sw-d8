<p><a name="Divide"></a><font face="Times New Roman, Times, serif" size="4"><b>The divide-and-conquer strategy</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
In an otherwise excellent article, Lee Sustar writes, "George W. Bush called on Kurds and Shiites to rebel against Saddam Hussein in the closing days of the Gulf War--but allowed Saddam to crush the revolt, rather than see Iraq break up." ("What the U.S. has in store for Iraq," <I>SW</I>, June 4)</P>
<P>I think the U.S. would want to see Iraq "break up." The U.S. has a history of dividing and conquering. After all, earlier in the same article, Sustar points out that the U.S. "leaders" who have previously sown the seeds of division in countries like Afghanistan and Lebanon are now choosing who will be filling the top posts in Iraq's government. </P>
<P>The real reason why the U.S. allowed the rebellion against Saddam to be crushed appears to be best explained by the words of Brent Scowcroft, Papa Bush's national security advisor. Scowcroft stated, "I envisioned a postwar government being a military government." </P>
<P>The U.S. would have preferred a new strongman in Iraq over allowing real democracy take place. Iraqi democracy would threaten U.S. corporations' profits since the Iraqi masses would not allow their country to be plundered and looted.<BR>
<B>Dominic Renda,</B> New York City</P>

