<p><a name="Half"></a><font face="Times New Roman, Times, serif" size="4"><b>A half-hearted strike won't do</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
After several weeks of working on an expired contract in the hopes of reaching an agreement with SBC, the Communications Workers of America (CWA) called a four-day strike, which began May 21. The anger and frustration of SBC workers was plain at the picket line, as they demanded the preservation of health care benefits and job security. </P>
<P>The four-day strike involved 100,000 workers in 13 states. A crowd of energized picketers, 200 to 300 strong, chanted slogans at the SBC office in downtown San Francisco. </P>
<P>Like the rest of the American working class, SBC workers are being squeezed out of their livelihoods for the sake of profits. Indeed, SBC is the second-largest telecommunications company in the United States, which earned $8.5 billion in profits last year alone! </P>
<P>And while workers are being asked to pay more for their health care insurance, SBC CEO Ed Whitacres received a pay increase of 93 percent. His 2003 compensation was $19.5 million. As one picketer summed it up: "They got the money; they just want to keep it for themselves."</P>
<P>Although the four-day strike was a good start, history has shown that it takes a lot more to win at the bargaining table. This was plain to picketer Vera Van Hook who explained: "I think the idea of going out Friday and Monday makes sense because these are our busiest days, but frankly, if we are really going to get what we want, we're going to have to be out longer."</P>
<P>It is time for workers to get organized and fight. There is a rich legacy of workers struggles in this country alone to learn the lessons of the past and to move forward with more consistency and confidence.<BR>
<B>Abraham Gutierrez,</B> San Francisco</P>

