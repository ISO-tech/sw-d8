<P><font face="Times New Roman, Times, serif" size="4">NLRB takes aim at "card check" deals in union organizing</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>A stacked deck against unions</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 25, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">GEORGE BUSH'S National Labor Relations Board (NLRB) is trying out a new assault on unions. Earlier in June, the NLRB reported that it would take a "critical look" at so-called "card check" agreements in labor contracts that enable workers to win union recognition by having a majority sign authorization cards, rather than going through a certification election.</P>
<P>Labor rightly sees certification votes as a deck stacked against them. The election process allows employers to stall organizing campaigns for years with legal challenges and illegal harassment--and then to defy union victories for longer still.</P>
<P>Card-check agreements allow labor to win representation more easily for workers at companies that have pledged not to interfere. Stewart Acuff, organizing director for the AFL-CIO, says that unions have gained a majority of their new members in recent years through card check.</P>
<P>On June 7, the NLRB decided in a 3-2 vote won by its Republican majority to review two card check agreements negotiated by the United Auto Workers union at auto parts makers Dana Corp. and Metaldyne Corp. Two days later, the panel agreed to consider a petition filed against the United Steelworkers over a union drive at Cequent Towing Products.</P>
<P>The three Republicans on the NLRB are all anti-union goons. For example, the board's chair, Robert Battista, is a Detroit lawyer from the union-busting firm Butzel Long who once represented the <I>Detroit News </I>and <I>Free Press </I>during a bitter strike in the mid-1990s.</P>
<P>Meanwhile, lurking behind all three cases is a Virginia-based antiunion group called the National Right to Work Legal Defense Foundation. "It's a shame and a disgrace," Acuff said. "Workers should not have to exercise an extraordinary level of courage to express support for a union, but that's the way it is in the United States now."</P>
<P>The board could give management a green light to go after unions by sanctioning the right to launch immediate decertification drives, instead of granting the typical one-year grace period after a union wins card-check recognition. Typically, this kind of "review" would take the NLRB a year or more to complete. "But I wouldn't be surprised if they tried to get it done by Election Day," said Jon Hiatt, general counsel for the labor federation.</P>


