<P><font face="Times New Roman, Times, serif" size="5"><b>Torture cover-up unravels</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 25, 2004 | Page 1</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE PENTAGON'S expert on running a concentration camp flew into Baghdad to give advice. "They're like dogs," counseled Major Gen. Geoffrey Miller, who recommended the torture methods he has used as overseer of the Camp X-Ray at Guant&aacute;namo Bay, Cuba.</P>
<P>"If you allow them to believe at any point that they're more than a dog, then you've lost control of them." Gen. Janis Karpinski--who oversaw military police at Iraq's Abu Ghraib prison until she was replaced by Miller--recalled Miller's words for reporters in June, driving the torture scandal to new heights.</P>
<P>With court-martial hearings for U.S. soldiers accused of torturing Iraqi prisoners set to drag through the summer and into October, the Bush administration faces the prospect of damaging torture revelations during the run-up to November's presidential elections. What's more, Col. James Pohl, a military judge, designated Abu Ghraib a "crime scene" that can't be destroyed--something George Bush proposed as a symbolic gesture after the torture first came to light.</P>
<P>Pohl also ruled that the accused soldiers' defense attorneys, who are trying to show that the blame lies higher up the chain of command, will be allowed to call high-ranking military commanders to the witness stand--including Gen. John Abizaid, commander of the U.S. Central Command; Lt. Gen. Ricardo Sanchez, the former senior commander in Iraq; and Lt. Gen. Thomas F. Metz, who oversees daily military operations. This will make it harder for the Bush administration to cling to its favorite excuse for the torture--that rogue troops acted against orders when they subjected Iraqi detainees to sexual humiliation, beatings and other abuses.</P>
<P>Even the mainstream media has begun to ask questions. "[N]early all of the punishment meted out so far in the torture scandal has involved underlings, despite evidence that responsibility for creating or tolerating a climate of abuse rests at the top," wrote the <I>St. Louis Post-Dispatch </I>in an editorial.</P>
<P>The torture of Iraqi detainees by itself would be enough to turn Iraqis against the occupation, but Washington is responsible for other atrocities. "Torture is only the tip of the iceberg," said Roger Normand, an international lawyer who directs the Center for Economic and Social Rights. "From unlawful killings, mass arrests and collective punishment to outright theft and pillage, the U.S. is violating almost every law intended to protect civilians living under foreign military occupation."</P>
<P>Washington's terror is coming from the skies, too. On June 19--in a duplication of Israel's tactics in its war on Palestinians--the U.S. launched two missiles at several houses in Falluja, claiming that Abu Musab al-Zarqawi, an Iraqi with supposed ties to the al-Qaeda network, was inside.</P>
<P>The air strike killed at least 22 Iraqis. But according to residents and medical personnel, the victims were largely women and children--and U.S. officials didn't claim the raid was a success, nor offer any apology.</P>
<P>Outraged residents of Falluja accused U.S. forces of trying to inflict maximum damage by firing two missiles.  "The number of casualties is so high because after the first missile, we jumped to rescue the victims," said Wissam Ali Hamad. "The second missile killed those trying to carry out the rescue."</P>
<P>But this didn't stop Iyad Allawi--the man picked to head Iraq's new puppet government that will be installed as of June 30--from defending the air strike. "We welcome this hit on terrorists anywhere in Iraq," Allawi told a news conference.</P>
<P>With a new survey commissioned by the Coalition Provisional Authority showing that 92 percent of Iraqis see U.S. forces as an occupying power, it's no wonder that Allawi's collaboration with Washington has insured his widespread hatred by Iraqis--61 percent of Iraqis are somewhat or strongly opposed to him.</P>
<P>Iraqis deserve the right to determine their own future. That's why activists in a number of cities have planned demonstrations on June 30--to protest the U.S. scheme to install a puppet government to cover its drive for oil and empire.</P>


