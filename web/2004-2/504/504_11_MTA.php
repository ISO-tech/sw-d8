<P><font face="Times New Roman, Times, serif" size="5"><b>Metropolitan Transit Authority</b></font></p>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Peter Lamphere</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 25, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Two weeks ago, New York City subway motorman Kevin Harrington was told he could no longer drive his train. The reason?  He is an observant Sikh who wears a turban, which doesn't match the Metropolitan Transit Authority (MTA) dress code.</P>
<P>After having been reassigned to a subway yard moving trains, the two-decade veteran quickly mounted a publicity campaign exposing the MTA's discriminatory decision. A few days later, after a series of grievances and newspaper articles, the MTA president rescinded the decision.</P>
<P>The public support was enormous. "When I returned to work," Harrington says, "I received hundreds of thumbs up from children to oldsters in the Jewish/West Indian community of Crown Heights in Brooklyn. The response was overwhelming--both West Indians and Orthodox-Hasidic Jews feel the city discriminates against them as it did against me."</P>
<P>This is not the first time that the MTA has discriminated against its workers. Last year, three Muslim women bus drivers were reassigned to moving buses between garages because they refused to remove their religious headdresses, called khimars. As Transport Workers Union Local 100 President Roger Toussaint put it, "The TA's obsession with religious headgear is an attempt to exploit 9/11 and promote hysteria."</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>"I'm not a slave"</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">New York City subway motorman KEVIN HARRINGTON spoke to <I>Socialist Worker </I>about how he won his campaign against MTA's discriminatory practices.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><P><B>WHAT DID you do did when they told you couldn't drive because you wore a turban?</B></P>

<P>THE FIRST thing I said was, "You're taking away my seniority and civil service right. I'm not a second class citizen; I'm not a slave. If you want to take that away, you're going to have a fight." Then I immediately went to my union.  On the second day, they told me, "You don't have to drive trains, go to your terminal."</P>
<P>So I went to my terminal and spent all day calling up the press. I decided I wasn't going to play footsie with the lawyers, I was going to go to the people of New York City--so I called up the tabloids. I knew there are lots of people out there who know the city government is bigoted.</P>

<P><B>WHAT WAS the response?</B> </P>

<P><I>NEWSDAY </I>PUBLISHED articles every day of the week--and wrote an editorial against the racist policy against turbans. The story even got into the <I>Hindustani Times.</I></P>
<P>Everyone from the media kept asking me "How long have you been in this country?" I kept having to explain I was born in New York. One right-wing radio host, Curtis Sliwa, said I should go back to India.  My response is that I would be happy to go back to Flushing [in Queens], but I can't afford the rents.</P>

<P><B>WHY DO you think you got the MTA to back down?</B> </P>

<P>I TOOK an activist point of view: I refused to be separated from my union and I refused to be separated from the people of New York.</P>

