<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">White House tries to deflect attention from its war crimes</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The world super-terrorist</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 25, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"THE FREE world cannot be intimidated by the brutal action of these barbaric people." That was George W. Bush's response to the June 22 killing of a South Korean interpreter who had been kidnapped by Islamist militants in Baghdad--only a few days after photos of an American engineer who had been captured in Saudi Arabia and beheaded were shown over Arab television.</P>
<P>Bush has a lot of nerve calling anyone a barbarian. After killing an untold number of Iraqis in its bloody war and occupation, the Bush administration is trying to deflect attention from its own war crimes.</P>
<P>The White House cynically hopes that it can gain more credibility for its "war on terror" by whipping up anti-Arab and anti-Muslim racism. But they can't stop the lies that they told to go to war on Iraq from unraveling.</P>
<P>Days after Dick Cheney told an audience at the James Madison Institute, a conservative, Florida-based think tank, that Saddam Hussein "had long established ties with al-Qaeda," the commission investigating the September 11 hijackings had their own announcement.</P>
<P>"We have no credible evidence that Iraq and al-Qaeda cooperated on attacks against the United States," their report said. "Bin Laden is said to have requested space to establish training camps, as well as assistance in procuring weapons, but Iraq apparently never responded." In addition, the commission dispelled claims that officials in the Saudi Arabian government funded al-Qaeda--a recurring accusation of Bush <I>and </I>Democratic presidential candidate John Kerry.</P>
<P>In the end, however, the commission will likely recommend greater security, greater military force and greater repression to defend the U.S. against terrorism. But this won't get rid of terrorism any more than the war on Iraq has.</P>
<P>If the commission wants to look at the biggest backer of terrorism, it should look at the U.S. government. In the 1980s, the U.S partnered up with Saudi Arabia to fund Islamist resistance in Afghanistan against the invasion by Washington's archenemy, the former USSR. One of the leaders of that struggle was a Saudi businessman named Osama bin Laden.</P>
<P>In the early 1990s, an exile organization called the Iraqi National Accord, under the direction of the CIA, tried to overthrow Saddam Hussein by planting bombs and sabotaging government facilities. According to the Iraqi government at the time, the group was responsible for an explosion in a movie theater, resulting in many civilian casualties. Today, the leader of that group--Iyad Allawi--is the U.S. government's choice for the new prime minister of Iraq.</P>
<P>And when the U.S. government isn't backing terrorists directly, it's committing terrorism of its own. Can there be any other word to describe the indiscriminate bombings, systematic torture and cold-blooded murder of civilians--all the things U.S. forces are responsible for in Iraq--than terrorism? And with each act of terrorism in the name of wiping out terrorism, and each act of extremism in the name of combating extremism, the U.S. sets the stage for more violence.</P>
<P>Bush's extremist crusade against terrorism makes the world a more, not less, dangerous place.  We have to oppose Bush's barbaric "war on terror" and expose it for what it is--an excuse for strengthening U.S. empire in Iraq and around the globe.</P>


