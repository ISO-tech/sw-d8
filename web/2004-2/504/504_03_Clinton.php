<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Sex, lies and Bill Clinton's book</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 25, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3"><I>MY LIFE </I>is an extraordinary book that should be read and reread by every socialist. Of course, we mean <I>My Life </I>by the Russian revolutionary Leon Trotsky. <I>My Life </I>by Bill Clinton, on the other hand, is pompous piece of junk--lies and distortions served up with Clinton's teary-eyed smile, rather than trademark snarl that we're used to from George Bush.</P>
<P>Since starting the whirlwind publicity tour to promote his autobiography, Clinton has done his best to seem high-minded. He claims to be interested in the "big issues" he faced while in the White House--the Middle East, Kosovo, health care. But predictably, the headlines about Clinton revolve around the sex scandal that almost drove him from the White House.</P>
<P>No interview or talk show appearance was complete without a somber Clinton recounting how sorry he is for his affair with Monica Lewinsky. This from a man who had the gall to lecture single mothers living in poverty about "morality" and "family values." </P>
<P>But consensual sex in the Oval Office wasn't a crime. Bill Clinton's real crime was his part in Corporate America's war on working people and the poor. His real crime was to continue the U.S. genocide against the Iraqi people with an eight-year-long string of military attacks and a devastating economic blockade. </P>
<P>His real crime was to leave behind a trail of broken promises on all of the programs or stands he promised to take in defense of unions, women's rights, gays and lesbians, civil rights for African Americans, and on and on.</P>
<P>In his interview with CBS News' Dan Rather to plug <I>My Life, </I>Clinton claimed that his two great "accomplishments" were "the creation of 22 million jobs" during his presidency and the toppling of Serbian President Slobodan Milosevic after the NATO war. As usual, Clinton's puffed-up rhetoric had only a passing acquaintance with the truth. The famed job creation of the "Clinton recovery" obscured the fact that large numbers of those jobs were concentrated in low-wage sectors. Corporate America, meanwhile, continued swinging the layoff ax even as it enjoyed record profits.</P>
<P>The 1990s were a bonanza for the super-rich. As liberal economist Lester Thurow wrote halfway through the decade: "No country without a revolution or a military defeat and subsequent occupation has ever experienced such a sharp shift in the distribution of earnings as America has in the last generation."</P>
<P>As for the 1999 NATO war over Kosovo, Clinton claimed that he was unleashing the U.S. military machine to stop the ethnic cleansing of Kosvar Albanians by Serbs. But the NATO slaughter only escalated the refugee crisis in Kosovo. </P>
<P>And when the war ended, Albanians carried out a reverse ethnic cleansing against Serbs in Kosovo--under the noses of U.S. and other Western "peacekeepers." The dictator Milosevic was overthrown--but more than a year after the war, and as a result of a mass uprising of ordinary Serbs, not anything that Bill Clinton did.</P>
<P>Clinton's book and publicity tour are designed to buff the former president's image--nothing more. Democrats hope the Clinton hype could inject some excitement into the brain-dead campaign of John Kerry--who is every bit as conservative as Clinton, but less capable of appearing to be an actual human being when he comes in contact with voters.</P>
<P>The real legacy of Clinton should be an object lesson to anyone willing to put their hopes in Kerry. As Malcolm X liked to say, if you put the Democrats first, they will always put you last.</P>


