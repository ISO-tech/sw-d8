<P><font face="Times New Roman, Times, serif" size="4">Activist Camejo chosen as running mate</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Left Green on Nader ticket</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 25, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">RALPH NADER has chosen leading Green Party member Peter Camejo as his running mate in the 2004 presidential election. The announcement came a few days before the Green Party was set to meet at its convention in Milwaukee to choose a presidential candidate.</P>
<P>Nader, who was the party's presidential candidate in 2000, has said that he doesn't want the Greens' nomination, but wants its endorsement, which could help him get on the ballot in 22 states and the District of Columbia.</P>
<P>Camejo and his supporters in the Green Party hope to win that endorsement for Nader at the convention, overcoming a challenge from David Cobb, who is campaigning for the Green's presidential nomination by advocating a "safe state" strategy of not challenging the Democrats' nominee John Kerry in states where a Green vote could tip the balance for Bush.</P>
<P>Camejo ran two Green Party campaigns for governor of California that won hundreds of thousands of votes--first in the 2002 general election and then the 2003 special recall election that drove Gray Davis out of office.</P>
<P>He stole the show in several debates during the recall election campaign, speaking out for universal health care and affordable housing and resistance to the Washington war machine. Camejo put forward a simple solution to the state's huge budget deficit that underlay the recall vote--tax the rich.</P>
<P>Camejo's political activism dates back to the 1960s, when he marched for civil rights in the U.S. South and was kicked out of the University of California-Berkeley for his antiwar activism. As a member of the Socialist Workers Party, he ran for president in 1976. And he has a longstanding record of fighting for immigrant rights and against racism in the U.S.</P>
<P>At the press conference where his vice presidential candidacy was announced, Camejo insisted that the U.S. war on Iraq was the central issue of the 2004 election. Declaring that the U.S. has "perhaps the most backward electoral system" among industrial countries, Camejo told reporters, "It is amazing to watch Democrats go out and campaign telling people not to vote for who they're for, not to vote for the platform they want, but to vote against what they believe in."</P>
<P>Camejo called the Democrats' presidential candidate John Kerry "a guy who will come in and set it up for the Bushes to come back--because the Democrats never challenge the underlying premises of what the Republicans are doing."</P>
<P>Despite the onslaught of Democratic Party slanders against him, Nader continues to win 6 or 7 percent support in some recent national opinion polls. This is a sign of the disgust with the candidates of the two mainstream parties--in particular, on the issue of the U.S. war and occupation of Iraq, which Nader is the only major candidate to oppose. According to the <I>Los Angeles Times, </I>"Nader's selection of Camejo gave further shape to a left-leaning, antiwar campaign."</P>


