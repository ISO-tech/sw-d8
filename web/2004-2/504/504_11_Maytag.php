<P><font face="Times New Roman, Times, serif" size="4">1,500 UAW members walk out over health care</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Big battle at Maytag</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 25, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEWTON, Iowa--More than 1,500 workers walked off the job at the big Maytag plant here June 10 in a fight that centers on health care costs. Members of United Auto Workers (UAW) Local 997--which has 700 on layoff--went on strike after the company insisted on health care cost increases that would increase out-of-pocket health care costs to $4,000 per year--not including co-pays. </P>
<P>The company is trying to impose this shift in costs even though workers agreed more than 20 years ago to divert cost-of-living allowance (COLA) payments into the company's benefits funds--a total of $5,300 per worker per year. Now Maytag--whose corporate headquarters is in Newton--wants workers to pay more <I>and </I>keep its hands on the diverted funds.</P>
<P>Pay increases are also a major issue. Rather than agree to an increase in wages, the company wants all compensation to be paid in lump sums, which means that the money is never calculated into the base pay used as the basis for future increases in wages and benefits.</P>
<P>Management also wants to freeze incentive pay based on productivity, despite recent increases in output by individuals. The company's proposed pension increases for current retirees would be eaten up by increases in health care costs, and the company won't make matching contributions to all workers in its proposed 401(k) plan.</P>
<P>Maytag is playing hardball. Last October, the company announced it was closing its refrigeration plant in Galesburg, Ill., and moving production to Reynosa, Mexico, where workers will earn about $2 per hour. Now, in Newton, management is calculating that the Galesburg shutdown will intimidate workers.</P>
<P>With one in 10 people in Newton put out of work by the strike, the pressure on union members is immense. Soon after the strike began, the company sent a letter to some 440 laid-off workers asking them if they were available to work--adding that if they do not, they would be considered on strike.</P>
<P>Then, the company took out newspaper ads to try to pressure workers over the heads of local union leaders. By winning a battle in Newton, organized labor could show that unions won't keep rolling over at employers' attempts to push rising health care costs onto workers' backs. Maytag workers need our support.</P>

<I><P>For more information about the strike, go to www.uaw997.org.</I> </P>

