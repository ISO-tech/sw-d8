<font face="Times New Roman, Times, serif" size="5"><b>The candidates pick the voters</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 29, 2004 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">THE PRESIDENTIAL election may be "too close to call" in the opinion polls, but we already know who is going to win on November 2. <p>
He's going to be a Skull-and-Bones man from Yale, the scion of a wealthy New England family. He supported the wars in Afghanistan and Iraq, the USA PATRIOT Act and the No Child Left Behind Act, while opposing the right of gays and lesbians to be married. And he completely supports the Israeli government's war against the Palestinian people.<p>
Of course, we know who the winning candidate is because the foregoing profile could describe either John Kerry or George W. Bush. This is yet another example of an election without a choice on most of the major issues on which elections are supposed to be fought.<p>
In political science textbooks, elections are supposed to represent an expression of the popular will. What the voters want is supposed to drive the election.<p>
Yet when you look at the way the two mainstream parties conduct elections in the U.S. today, you see something quite different. Instead, the parties try to mold the electorate for their purposes. Their messages establish the boundaries of what the election will be fought over.<p>
Besides the litany of issues on which Kerry and Bush agree, the two big business parties have decided that there will be no discussion of a genuine national health care plan in the election, despite the fact that polls show that 60 percent or more Americans support one. So voters face the absurd reality that a program that most of them support--and is considered mainstream in every other capitalist democracy--will be championed by outgunned and marginalized third parties.<p>
The two parties have spent more than $1 billion between them to appeal to and mobilize voters, and their efforts may result in the highest voter turnout in decades. But they want to assure that the people who actually vote don't provide them with any unwanted surprises.<p>
Under the guise of "protecting the integrity of the ballot," Republicans are mounting a number of schemes to suppress turnout of reliable Democratic voters, such as African Americans, and new voters who might have recently registered so they could vote to oust Bush. Republicans in Ohio plan to challenge voters' credentials in polling places around the state. They hope to create so much confusion and delay at the polling places as to force Democratic voters to leave without voting.<p>
In states where Republicans have control of the election machinery, they'll use other tactics like disqualifying felons, moving polling places and/or mobilizing police to intimidate voters--all to make sure that the people whose votes are actually counted are the most Republican-friendly they can make them.<p>
The Democrats play a different role in the system. Their job is to head off the possibility of any alternative developing to their left that might compete for the allegiance of the most reliable Democratic voters--African Americans, unionized workers and women. That's why the Democrats have spent millions of dollars and deployed thousands of lawyers in an effort to knock Ralph Nader off the ballot in states across the country. <p>
The fact that they tried to knock Nader off the ballot even in the so-called safe states of California, Illinois and Massachusetts is evidence that they aren't just concerned about Nader "spoiling" Kerry's chances. This assures that voters in those populous states who want to vote for ending the Iraq occupation or for gay marriage or national health care won't have the chance to.<p>
In this way, the Democrats want to preserve themselves as the only "realistic" alternative for millions who want to get rid of Bush. This goes along with Kerry's so-called "centrist" campaign that assumes that the key to a Democratic victory is to shave off a few Bush voters with appeals to cultural conservatism.<p>
An aggressive campaign promising to address the economic insecurities and class inequality could actually give many millions more Americans, including millions in the so-called "red" states, a reason to vote. But that would raise working-class expectations--which is exactly the opposite of what big business wants from the Democratic Party.<p>
In all of these ways, big business has assured that most of the crucial decisions about the direction of the country have already been made before the voters show up at the polls on November 2.<p>

