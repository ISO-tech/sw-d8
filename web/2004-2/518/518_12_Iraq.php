<font face="Times New Roman, Times, serif" size="4">Iraq's opposition to occupation</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"All our people are resisting"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 29, 2004 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">U.S. WARPLANES have been flying nightly missions to drop 500-pound bombs on Falluja, and U.S. troops are perched on the outskirts of the city. But the Iraqi resistance refuses to back down.<p>
If anything, the mounting toll of civilians killed by the U.S. is creating a growing pool of support for resistance fighters. "If Americans approach the city, all of western Iraq from Falluja to the Jordanian border will turn into a combat zone," says Astil Mahmud, a Falluja resident who drives a passenger shuttle between the city and Baghdad. "All our people are in the resistance...The more people die, the more people volunteer."<p>
Washington's plan for January elections in Iraq--and Bush's desire to keep a lid on events in Iraq until after the U.S. election--means that U.S. forces are gearing up for an offensive to "pacify" this rebel stronghold.<p>
And with the military strategy set to enter a new phase, U.S. officials are also taking a new approach in their political strategy. Ever since the weapons of mass destruction justification for the invasion fell apart, the Bush administration has claimed that the occupation will bring "liberation," "freedom" and "democracy." Now, it's clear that this is every bit as much a lie.<p>
Behind the scenes, U.S. diplomats are urging six Iraqi political parties--including Ahmad Chalabi's recently rehabilitated Iraqi National Congress, a perpetual favorite of the Pentagon--that currently run the U.S.-backed interim government to form a united slate to insure their dominance in the January elections.<p>
U.S. officials concede that their use of money to influence the elections might be in bad taste, given the Bush mantra that Iraqis will control their own destiny. But they believe that preserving stability must come first--even if that means cementing the rule of U.S.-backed political parties with little support among Iraqis. "I see the arguments for stability now outweighing the calls for democracy," one official told the <i>Los Angeles Times,</i> saying that the time is right for "a negotiated resolution...a scaled-back democratic process."<p>
With these calculations lurking behind the U.S. presence, it's no wonder that resistance fighters targeted 49 Iraqi soldiers in training at a fake checkpoint 40 miles outside of Baghdad, killing them all. The operation depended for its success on inside information, opening up new doubts about how useful Iraqi forces will be in carrying out missions critical to maintaining U.S. rule.<p>
The recent spate of kidnappings, roadside bombs and attacks on the U.S.-fortified Green Zone in central Baghdad together create a sense of chaos that will cause further problems for the U.S.--because reconstruction efforts are impossible under these circumstances. The kidnapping of Margaret Hassan, an Irish-born aid worker who has lived in Iraq with her Iraqi husband for 30 years, has further accelerated the exodus of relief agencies.<p>
But the blame for this lies squarely with the U.S. Without the U.S. occupation, there would be no resistance. However much violence the rebel fighters use, it pales in comparison to the war crimes carried out by the U.S. on a continual basis.<p>
The only solution to the nightmare in Iraq is the immediate withdrawal of U.S. troops and a massive payment of reparations to Iraqis by the U.S. government that made their life a living hell.<p>

