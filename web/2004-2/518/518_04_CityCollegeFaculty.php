<font face="Times New Roman, Times, serif" size="5"><b>The key to victory for City College faculty</b></font><p>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 29, 2004 | Page 4</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
On October 19, Chicago City Colleges (CCC) faculty struck after the administration refused to budge on a contract that would all but abolish the hard-won gains made over the years by the Cook County College Teachers Union (CCCTU).<p>
Like many unions facing concessions and a whittling away of union power, the CCCTU has a once-proud history of struggle and solidarity. Between 1966 and 1978, seven strikes built what was one of Chicago's strongest public-sector unions.<p>
In 1966, against an injunction--and a suggestion from the <i>Chicago Tribune</i> that schools be closed "until law-observing replacements" could be found--workers stayed on the line. They won a 12-credit-hour load that the current administration wants to get rid of. In 1969, the CCC transferred two teachers involuntarily. The CCCTU struck, and the transfers were rescinded.<p>
The CCC came back to the bargaining table with the intention of rolling back the union, increasing workload and watering down the grievance process. In 1971, the CCCTU began a five-week strike and beat back the worst demands. Picket lines were attacked, and students occupied the chancellor's office to support the union. In 1975, the union faced down fines and an injunction that landed the union president in jail for five months to beat back demands on class sizes and overtime.<p>
In 1978, the union battled an attempt to increase workload by 25 percent. Knowing the CCC would threaten to cancel classes to intimidate faculty, the union prepared members. The city blinked first. Again, on balance, the union won.<p>
Now, the CCC thinks it can do what it was unable to do 30 years ago--get rid of the 12-credit-hour workload and undermine the union.<p>
The history of the CCCTU shows that solidarity is key and workers' power is on the picket line. As the former union president once said, "90 percent of what we got" in negotiations "was while we were on the picket line."<p>
To make pickets work, faculty, staff and students need to be united. Workers across Chicago are under attack, from transit workers facing layoffs to teachers at Northeastern Illinois University facing a contract battle of their own. They are watching to see who wins the eighth strike at the city colleges.<br>
<b>Adam Turl, </b>Chicago<p>

