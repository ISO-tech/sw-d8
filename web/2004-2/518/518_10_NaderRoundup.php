<font face="Times New Roman, Times, serif" size="5"><b>News from the Nader campaign</b></font><p>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 29, 2004 | Page 10</font></p>

<font face="Times New Roman, Times, serif" size="3">TAMARAC, Fla.--Supporters of John Kerry and John Edwards must be sensitive about their candidates' pro-war stance because they manhandled two protesters who dared to point it out at a campaign event on October 24.<p>
When Nader-Camejo supporters Jessica Kochick and Emily Brackett held up signs that read "Kerry/Edwards Supports War in Iraq" and "Vote Nader-Camejo," Edwards told them to move to the back of the room. When they didn't, a Kerry supporter took matters into his own hands, putting Kochick in a headlock.<p>
Instead of restraining the man, the police dragged Kochick away by her hands and feet. The two young women were charged with disorderly conduct--for holding up Nader signs.<p>
Before they were released, the two were interrogated by the Secret Service and held for hours in an undisclosed location for "security purposes." Kochick and Brackett are part of Nader's Corporate Crimebusters team of 20 vans that are fanning across the country to shine a spotlight on corporate greed.<p>
-- In <b>Burlington, Vt., </b>Nader's vice presidential running mate Peter Miguel Camejo spoke to students at the University of Vermont on October 20. He was joined by four local speakers, who discussed subjects ranging from the USA PATRIOT Act to local third-party politics and the occupation of Iraq.<p>
Camejo encouraged the audience of more than 100 to join the campaign to build a third party that can challenge the "two party dictatorship" in the U.S. He called for gay marriage and women's reproductive rights, illuminating Kerry and Bush's parallel views on both issues.<p>
<i>Mary Howland contributed to this report.</i> <p>

