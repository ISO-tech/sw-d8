<font face="Times New Roman, Times, serif" size="4">Breaking away from the Democrats:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"No support from the party we built"</b></font><p>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 29, 2004 | Page 6</font></p>

<font face="Times New Roman, Times, serif" size="3">NATIVO LOPEZ is president of the Mexican American Political Association (MAPA), one of the oldest and most respected political organizations representing Latinos, Chicanos and Mexican people in the U.S. Earlier this year, he chose to change his party affiliation from the Democratic Party to the Green Party. He is supporting the independent campaign of Ralph Nader and Peter Camejo.<p>
Lopez talked to SARAH KNOPP about his decision and the issues involved in Election 2004.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>WHAT ISSUES do you think are most important to your community in these elections?</b> <p>

THE QUESTIONS or issues most important to my community are: the war in Iraq and its prolongation under either administration, and the prospect of a military draft; the sluggishness of the economy and the receding real wage of our families; the decline of investment in education and the added obstacles to access to quality education and higher education; the continuation of an unjust immigration policy, specifically along the Mexico-U.S. border, and the average one death a day of a Mexican attempting to cross the border; the lack of political representation under both traditional political parties, and therefore, the sharp increase in independent voter registration among Latinos (a manifestation of the alienation among Latinos towards these parties); the sharp increase of the medically uninsured and underserved, and the price hikes for medical services; and the prospect of an all-out assault on a women's right to choose.<p>

<b>WHAT DO you think we should do after the election? Is your answer to this question different depending on whether Bush or Kerry wins?</b> <p>

MY FEELING about what should occur subsequent to the elections would apply under either party administration--organization, organization, organization of our community. We need to hold the government accountable for their actions and to obey the governed.<p>

<b>WHY DO you think the Democratic Party decided not to seek the endorsement of MAPA?</b> <p>

NOT ONLY MAPA, but any organization that is truly independent of the Democratic Party machine. They truly fear an unscripted appearance before the Latino community. If it is not a scripted rally driven by the consultants and sycophants of the candidate, they ignore and avoid the occasion and audience. This is an example of plasticity and falsehood that will eventually catch up with the centrists.<p>

<b>WHAT WAS the most important reason you changed your party affiliation?</b> <p>

FOR TOO long, I worked within the grassroots of the Democratic Party--not the party machine, nor with the elected officials, but building within the Latino community, driving voter registration campaigns, supporting local candidates, building grassroots leadership, promoting Latino political representation and mounting U.S. citizenship campaigns. <p>
We have never enjoyed legitimate and sincere support from the very party that we helped build, and in many cases, elected candidates from this party. It is an unresponsive party to the needs of the majorities and is driven by white male millionaires and their political consultants, who only seek to impose their political will on the majorities (including white workers)--and only support those candidates of color who they can control and manipulate for their own economic interest. <p>
It is a party that cannot be reformed nor any longer aspire to great leadership and reformation of the current political system and climate. It, too, is the party of globalization.<p>
It was time for me to move on and work to move my community beyond the Democratic Party. It is the only hope for our social progress.<p>
I have studied the political platform of the Green Party, and I agree with it 99 percent. I still covertly like cockfighting--an old Mexican tradition. Second, this party has an international affiliation, which I believe is important considering the current effects of globalization on the world. Third, it is diverse and still provides an opportunity to define an alternative progressive political direction for Mexicans, Mexican Americans and Latinos. Fourth, young Latinos are not impressed with the traditional political parties. They do not meet their needs, and young people have much to contribute to the progressive resolution of the world's problems.<p>

