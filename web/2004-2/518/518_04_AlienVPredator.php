<font face="Times New Roman, Times, serif" size="5"><b>Whoever wins, we lose</b></font><p>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 29, 2004 | Page 4</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
When they're done with each other, the winner will come after us.<p>

