The 2004 elections and the summer action movie <i>Alien v. Predator</i> have eerie parallels, as I'm sure many people have noticed. The <i>LA Weekly</i> quotes Nader supporter Karl Swinehart: "There's no question that the alien is Bush, just a slobbering monster. But the predator, that's Kerry. He's a chameleon who actually has a strategy and who's able to fool people into thinking he's something he's not. Whoever wins, we lose."<p>
"Unfair," Kerry supporters feebly cry out. "Kerry is better than Bush. They are not equally evil." I'll concede that they are not the same. But any look at history should make us think twice about assuming that the Democrat is less able to reap destruction.<p>
George W. Bush has killed tens of thousands in the war on Iraq. But his Democratic predecessor, Bill Clinton, holds the responsibility of having upheld a sanctions policy that killed over 1 million Iraqis. Try to explain to an Iraqi mother that Democratic starvation is better than Republican bombs.<p>
We know that Kerry has supported the war on Iraq and the USA PATRIOT Act, and that he has opposed same-sex marriage and driver's licenses for immigrants. On all these things, he and Bush are equally evil. But because the Democrats have the undeserved reputation of being our friends, we forget the damage that they can do by exploiting that relationship.<p>
Under Bill Clinton, every single group the Democrats claim as a "constituency" had conditions worsen. The poor saw the gap between themselves and the richest Americans increase ten-fold; 5 million more people lacked health care than at the beginning of Clinton's term; more Black men got prison sentences than college degrees; labor steadily declined in part due to job-cutting deals like NAFTA; gays and lesbians were kicked out of the military at a faster rate than ever before; women saw access to abortion dramatically decline; more immigrants died on the U.S.-Mexico border than in the entire 50-year history of this Berlin Wall; and the list goes on.<p>
Ralph Nader and Peter Camejo have dared to call out the corrupt duopoly of American politics. We should support them. <p>
A huge majority of the world opposes the occupation of Iraq. There are our allies. From Bolivia to India, workers are fighting neoliberalism. That is our hope. Millions of people in the richest country in the world have no health care, while $1 billion a week is spent on war. That is our fight.<p>
Should we decide which wrecking machine to support, or should we draw a line and stand firmly opposed to evil in all its degrees and incarnations?<br>
<b>Bill Neal, </b>Los Angeles<p>

