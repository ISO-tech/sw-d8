<font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The price of labor's lesser evilism</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Darrin Hoop, UFCW Local 1105</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 29, 2004 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">GEORGE BUSH has been as pro-business and anti-union as any president in U.S. history. He's passed tax cuts for the rich, banned any possible airline strikes, denied overtime pay for millions of workers, banned unions in the Department of Homeland Security, and used the Taft-Hartley Act against the International Longshore and Warehouse Union (ILWU). <p>
But on November 2, the labor movement must ask: Is John Kerry a real alternative to Bush? Are the Democrats really friends of labor? <p>
Kerry's record alone should raise serious questions. He voted for NAFTA, welfare "reform," the No Child Left Behind Act and the war in Iraq, and he has said nothing about abolishing the anti-labor Taft-Hartley Act. <p>
More importantly, the history of the Democrats shows that they don't deserve the "friends of labor" label. <p>
Consider President Franklin Roosevelt, who cemented labor's ties to the Democrats in the 1930s. As he himself said, "I am the best friend the profit system ever had." He granted some social reforms--including the right for workers to join unions--in the hope of preventing working-class rebellion. <p>
Yet according to the American Civil Liberties Union, in the first six months of Roosevelt's "New Deal," at least 15 strikers were killed and 200 injured. Hundreds were arrested, 40 anti-union injunctions were issued and troops were sent to crush at least six strikes. In 1934, some 52 strikers were murdered by forces hired by the government or private companies. In 1935, 20 state militias, most controlled by Democratic governors, were called out against strikers in 73 disputes. <p>
What's more, Roosevelt's jobs programs never provided jobs for more than 25 percent of the unemployed. Both Roosevelt and his successor, Harry Truman, tried to get Congress to give the president the power to draft into the armed forces any workers who struck against government-controlled workplaces. <p>
The Taft-Hartley Act was passed in June of 1947 over Truman's veto. It called for an 80-day cooling-off period where no strikes or slowdowns were permitted or fines and/or jail time were levied. The law banned "secondary boycotts"--that is, sympathy strikes--making solidarity illegal. International union officials were required to sign affidavits stating they weren't members of the Communist Party. <p>
Truman used Taft-Hartley 12 times in its first year. Then, in the 1948 presidential election, he campaigned to repeal it. But after the labor movement provided the key margin of victory, he used it nearly 50 more times in the next three years. <p>
Before Bush used Taft-Hartley against the ILWU in 2002, President Jimmy Carter was the last one to use it--unsuccessfully--against coal miners in the late 1970s. The Clinton administration used a similar anti-union law, the Railway Labor Act, 14 times to ban potential railroad and airline strikes, including a 1997 walkout by pilots at American Airlines. <p>
While the rhetoric of the Democrats is pro-labor, history shows a bipartisan class war. Labor's strategy of "partnership" with the Democrats and Corporate America has been a disaster. <p>
When Service Employees International Union (SEIU) President Andy Stern recently called the Democrats, "a hollow party," he correctly identified the problem. Unfortunately, SEIU and the rest of labor have fallen in line behind Kerry to the tune of tens of millions of dollars. <p>
In this election, Ralph Nader is the only legitimate pro-worker and anti-corporate candidate--and he calls for the repeal of Taft-Hartley. <p>
In addition to backing Nader, labor would be better off looking to the radicalism of the 1930s for answers on how to rebuild our movement. Then, millions of workers united to build the mass industrial unions in spite of the efforts of the two parties, business, the courts, the cops and the National Guard to smash the union drives.<p>
It was workers' direct action--not Roosevelt and the Democrats--that built the unions in the 1930s. Learning from this history will be key to politically arming the labor movement to move forward no matter who wins on November 2. <p>

