<p><a name="Moshing"></a><font face="Times New Roman, Times, serif" size="4"><b>Moshing against Washington's war</b></font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
"Let the President answer on high anarchy/Strap him with AK-47, let him go/Fight his own war, let him impress daddy that way/No more blood for oil, we got our own battles to fight on our soil..."<p>
It ain't Dead Prez, it ain't Public Enemy, and it ain't Rage Against the Machine. It's the gay-basher extraordinaire Eminem, who's new song "Mosh" recently debuted on MTV. This time, though, socialists can nod their heads in approval.<p>
Dark and apocalyptic, with a slow marching beat, the song and video reflect the doom and gloom people have had to face over the last several years. A montage of news clippings from the last year are pasted on a wall--including "Bush Declares War" and "Sick Wounded Troops Held in Squalor," among others. <p>
The video shows many class conclusions being drawn as a result. A soldier comes home to his family, only to get a letter sending him back to Iraq. His wife gets a notice of eviction. A Black man gets stopped and harassed by the police.<p>
Overwhelmingly, the message here is "Vote November 2," and the video, although not the song, says so explicitly at several points. Yet with lyrics telling people to "Rebel with a rebel yell, raise hell we gonna let 'em know/Stomp, push up, mush, fuck Bush, until they bring our troops home," there's more under the surface than just putting the ballot in the box.<p>
While there are contradictions in his conclusion, Eminem's video is a reflection of where many people are--and draws conclusions that many people haven't.<p>
No one should begin mistaking the homophobic, misogynistic, and at times racist Eminem for a progressive. But this is the type of cultural phenomenon we need more of. If more platinum-selling performers with huge audiences are going to start making antiwar videos and drawing class conclusions, then we'll take it.<br>
<b>Nick Chin, </b>Washington, D.C.<p>

