<p><a name="Stupid"></a><font face="Times New Roman, Times, serif" size="4"><b>Stupid voters are to blame for Bush</b></font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
I'm a progressive anarcho-syndicalist who doesn't understand why the left has such a hard time blaming the right people for the deplorable state of things in this country.<p>
The blame for the present colossal defeat of progressive ideas by conservative politicians rests squarely on the shoulders of the voters who voted for them! The mess we now find ourselves in is the responsibility of a large number of voters who are bigoted, religious, ignorant (e.g., Saddam was responsible for 9-11) and totally self-absorbed.<p>
What this election means to me is that the U.S. is definitely a divided country. It's divided between people who think and reflect about important things in the world, and people who don't. The sooner the left comes to terms with this very great problem, the better. <p>
Stupid voters are to blame for re-electing Bush and company. And the left is to blame for not demanding justification for the decisions made by so many voters. Until we face the fact that voting is a responsibility and not a right, we will continue to have election results like this one, where votes are cast for self-serving, jingoist reasons and where, the day after, we all go about our business like it was a job well done. <p>
What causes the blame game is that most people feel it's unacceptable to question people's choice for president. Instead of bad choices, then, fault is said to lie with Kerry or the Anybody-But-Bush crowd and those who are running the big show. <p>
Bull! We ought to demand to know from people their reasons for voting, to demand explanations that make sense, and if that's not forthcoming, then push and push and push! <p>
Of course, if this actually happened, all civility will be lost in our "great" land. Who knows where this would lead?<br>
<b>Douglas Braaten, </b>From the Internet<p>
