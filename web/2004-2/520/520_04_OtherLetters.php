<font face="Times New Roman, Times, serif" size="4"><b>The stadium stick-up in Washington, D.C.</b></font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
On October 28, more than 300 people gathered at a public hearing to protest public dollars being used to build a new baseball stadium in Washington, D.C.<p>
Along with the $530 million cost of the stadium, the District of Columbia will have to pay for all of the stadium's future renovations and repairs, all repairs and renovations to bridges and roads around the stadium, and all security and traffic control around the stadium. The team will get all revenues from parking, tickets, concessions, souvenirs, naming rights and the media. <p>
Many of the people who came out to the rally believe that more money should go to education, affordable housing, libraries, health care and community programs. One in 10 D.C. residents have no health insurance and the city has the highest per capita AIDS rate of any major city in America. The percent of people living in poverty is almost twice that of the national percentage.<p>
Christopher Greene, a high school student at Eastern Senior High School, remarked about the poor conditions at his school: "My walls are coming apart...and we have books from 1996."<p>
Former Mayor Marion Berry, who is a candidate for Ward 8, is opposed to the funding of the stadium. He said, "This is the biggest stickup by Major League Baseball since Jesse James was doing train robberies."<p>
Democratic Mayor Anthony Williams avoided the joint public hearing and has only planned one community discussion on the plan in the future. The Washington Interfaith Network, a community-based organization for the wellbeing of others, has pushed a plan to several council members to give more than $100 million to the community before putting any money into the baseball stadium.<p>
Clearly from the list of people that spoke, a majority say no to public funding for a new baseball stadium. As Tommy Wells, a school board member, said, "[W]e need the money to fix heat and air conditioning and for our schools to be taken care of."<br>
<b>Sheri Pegram, </b>Washington, D.C.<p>
<i>Visit www.nodctaxesforbaseball.org on the Web for information on how to oppose the stadium.</i><p>
