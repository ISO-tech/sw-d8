<p><a name="Democrats"></a><font face="Times New Roman, Times, serif" size="4"><b>We need more than just the Democrats</b></font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
I really enjoyed reading Sharon Smith's article ("Right-wing republic?" November 5). I totally agree that we desperately need a new party, or at least a new direction within the Democratic Party, which appeals to working-class Americans who are fed up with the war and job losses and all.<p>
It's kind of scary to think about what that will require, and I haven't typically been a very political person, but I'm thinking I'd better get a lot more involved in politics a lot earlier than right before the next election. I suppose right now is the time to get involved, so we have someone to vote for next time around who really represents us. Thanks for the article.<p>
<b>Dave Hershberger, </b>Pasadena, Calif.<p>
