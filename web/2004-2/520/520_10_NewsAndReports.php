<font face="Times New Roman, Times, serif" size="5"><b>News and reports</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">November 12, 2004 | Page 10</font><P>

<font face="Times New Roman, Times, serif" size="4"><b>Justice for Palestine</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Kristen Robinson</b></font><p>

<font face="Times New Roman, Times, serif" size="3">DURHAM, N.C.--The fourth annual conference of the Palestine Solidarity Movement took place as planned on the weekend of October 15-17. More than 650 people attended the events, including more than 200 Duke University students. <p>
This was a clear victory for conference organizers and attendees, who faced various attempts of intimidation and disruption in organizing the three-day event.<p>
In the weeks leading up to the conference, pro-Zionist groups used threats of violence to try to pressure Duke to cancel the event. Just a week before the conference, an offensive e-mail was sent out to several thousand Duke students from a conference organizer's e-mail address. After investigating, Duke's security staff found that the e-mail originated in California.<p>
Conference attendees walking to workshops were "greeted" with pro-Zionist graffiti on walkways and university buildings. According to the conference's Web site, there were a few attempts at disruption from protesters in meetings, but in general, the hype turned out to be just that. Only a dozen or so pro-Zionists showed up to protest the conference--and they were completely marginal to the weekend's events. <p>
The conference brought out many important organizational and tactical debates, including how to link the occupation of Palestine to the occupation of Iraq within the U.S. antiwar movement. The conference covered topics ranging from the history of Zionism to several workshops about how to organize a campus divestment campaign. <p>
"When people talk about the occupation of Palestine or their concern of Israel as a military state, or how much the U.S. spends on Israel, or what the U.S. is doing to the world, they look at the symptoms of the problem," explained long-time Palestinian activist Rajaie Qubain about the chief contribution of the conference in his opinion. "The disease of Israel is racism and unless we address it as such, we will never solve these problems."<p>
More than a few workshops ended in debates about a range of issues, including the movement's stance on violence, whether there must be a one- or two-state solution to the conflict and more. <p>
Perhaps the biggest strength of the conference was that many people had never been to such an event before. Some attendees complained that there was not enough analysis of why the U.S. supports Israel and how the two occupations are linked. While this was undoubtedly a weakness, overall the conference was useful and constructive--and it showed that the movement for Palestinian rights refuses to be silenced or intimidated.<p>

