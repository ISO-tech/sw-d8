<font face="Times New Roman, Times, serif" size="4">Faculty and staff stop concessions at Chicago City Colleges</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Teachers win their strike</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By a CCCTU member</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 12, 2004 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--The full-time teachers and professional staff at the Chicago City Colleges (CCC) successfully resisted the attempts of the administration to impose a draconian contract that would have effectively crushed their union--the Cook County College Teachers Union (CCCTU)/American Federation of Teachers Local 1600.<p>
On November 7--after nearly three weeks on strike--union members approved by a 95 percent margin a four-year contract devoid of most of the concessions originally demanded by the administration.<p>
The administration originally wanted the union to accept a scheme in which members would pay up to 20 percent of health care premiums, with drastic increases in out-of-pocket cost caps. This would have cost the teachers and staff thousands of dollars per year. The new contract calls for a fixed annual contribution that gradually increases over the life of the contract--although in the last two years, the contributions increase at a faster rate.<p>
A provision that would have required tenured teachers to publish or risk losing their jobs--something that would have severely eroded the power of the union to protect the teachers' jobs--was struck down. The "no reprisals clause" in the contract was renewed--which prevents the administration from victimizing union members, staff belonging to other unions, part-time teachers and students who supported the strike.<p>
The contract also made significant gains for nursing teachers, who will be paid for every contact hour of clinical work beginning next fall. And part-time professionals got their first contract ever--after 16 months of negotiations--putting them on par with full-time professionals, who got a 4 percent pay increase per year.<p>
Class sizes will remain unchanged, though the administration had hoped to increase them up to 42 students per classroom. While the new contract kept the teaching loads unchanged for the life of the contract, it conceded to an increase from 12 to 15 credit hours per semester for all faculty when the contract expires.<p>
This is a point of contention for new faculty, who already teach this amount--as a concession made in a previous contract. New faculty find this load a heavy burden that not only makes them feel overworked, but also undermines the quality of teaching they want to offer their students.<p>
Another weak point in the contract is the pay increase for the faculty, which alternates between 4.25 and 3.2 percent over the four years of the contract--averaging out to about 3.75 percent per year. However, these increases are to be granted in two installments every year--effectively reducing the average increase to about 2.6 percent per year over the life of the contract because the largest fraction of the pay raise happens during the second half of each year.<p>
But on the whole, the contract is a victory because it stopped in its tracks the fierce attacks of an administration that used every dirty trick in the book to intimidate teachers into going back to work defeated. Significantly, fewer than 10 union members--out of 775--crossed the picket line. <p>
Lively pickets and solidarity rallies played an important role in this victory. On November 3, about 150 CCC teachers and students met to hold an evening vigil in defense of public education in downtown Millennium Park--and were joined by 300 antiwar demonstrators. The following morning, more than 400 striking faculty and staff, students and supporters surrounded CCC headquarters. As about 20 challenged the school board during a meeting, more than 100 took over the building lobby. <p>
This activism and support threw the administration off balance. They had calculated that the CCCTU--after two previous concessionary contracts--was weakened and would be easy to bust.<p>
In fact, the strike has reinvigorated the membership, and many believe that one of the most important effects of the strike is the renewed sense of solidarity. And many members now realize that they will need to get more involved in the union to help transform it into a more effective force to defend their interests.<p>
<i>Orlando Sep&uacute;lveda and Carole Ramsden contributed to this report.</i> <p>

