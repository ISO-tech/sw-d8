<font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Bush's deadly assault on Falluja</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Standing up against their war crimes</b></font><p>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 12, 2004 | Page 3</font></p>

<font face="Times New Roman, Times, serif" size="3">"I EARNED capital in the campaign, political capital, and now I intend to spend it," George W. Bush smirked at his first post-election press conference. And the first place Bush decided to spend his supposed "capital" was the Iraqi city of Falluja, where, as <i>Socialist Worker </i>went to press, the U.S. military had begun a punishing assault.<p>
The U.S. government's bloody war and occupation in the name of "liberation" have only produced more misery for Iraqis--and with it, a growing resistance.<p>
Now is a critical time for the antiwar movement to show its opposition to the U.S. occupation--and after months of election-season nothingness, many activists are ready. In the days after Bush's victory, hundreds of people gathered at local antiwar demonstrations across the country, and more protests are planned this week as an emergency response to the battering of Falluja. Anyone who opposes this war should join these demonstrations, large or small, in their city.<p>
The Bush gang claims its assault on Falluja is targeting a small nest of "terrorists." "Success in Falluja will deal a blow to the terrorists in the country and should move Iraq further away from a future of violence to one of freedom and opportunity for the Iraqi people," claimed Defense Secretary Donald Rumsfeld.<p>
But that's as much a lie as the administration's former fantasies about weapons of mass destruction. The real purpose of the offensive is to crush the city that symbolizes resistance in Iraq--and show that the U.S. government will stop at nothing to get what it wants.<p>
The Bush gang wants control over Iraq's oil--the second-largest known reserves in the world--but they also want to let it be known that they call the shots, in the Middle East and around the globe. Ever since September 11, the "war on terror" has been the pretext for Washington's drive to expand its military and economic power. The slaughter in Falluja today is no different.<p>
We have to confront the administration's lies about "taking on the terrorists." Indeed, the antiwar movement has only been made weaker by some of its leaders' acceptance of the proposition that the Iraq war was a "diversion" from a legitimate "war on terror."<p>
During the presidential election, many liberals and even radicals echoed this line, taken directly from prowar Democrat John Kerry's playbook. For example, in his endorsement of Kerry in the <i>Nation </i>magazine<i>, </i>David Corn argued, "There is no doubt that Kerry is as committed as the current Commander in Chief to protecting the U.S. from the threat posed by al-Qaeda and others."<p>
But this is exactly the point that the antiwar movement needs to challenge--that Washington's wars on either Iraq or Afghanistan had anything to do with protecting ordinary Americans.<p>
In their single-minded crusade to dump Bush, a majority of liberals and progressives compromised their antiwar principles. Not only were important resources wasted in getting out the vote for a prowar candidate, but the movement was set back politically because so many activists accepted the backward arguments offered in defense of Kerry's position.<p>
Author Arundhati Roy recently described the U.S. election this way: "The fire and brimstone of the U.S. election campaign was about who would make a better 'Commander-in-Chief' and a more effective manager of the American Empire. Democracy no longer offers voters real choice. Only specious choice."<p>
When the U.S. government attempts to criminalize the Iraqi resistance to occupation as "terrorism," antiwar forces have to stand up against the attack. And we have to oppose the idea that the U.S. has to "clean up the mess it made" in Iraq. The U.S. is incapable of "doing the right thing." That's why we call for troops out now.<p>
Taking up these questions is the way to build a stronger antiwar resistance in the U.S. <p>

