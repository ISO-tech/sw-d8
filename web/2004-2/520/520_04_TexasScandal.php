<font face="Times New Roman, Times, serif" size="4">Problems at Houston crime lab affect thousands</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Texas' deadly scandal</b></font><p>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 12, 2004 | Page 4</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker</i>,</b> <br>
A scandal of tremendous proportions has been unfolding in the Texas criminal justice system.<p>
A few years ago, it was discovered that the Houston police crime lab had some serious problems, including a leaky roof which had led to contamination of evidence. Subsequent investigations have found numerous other problems with the lab. <p>
The DNA division was shut down in 2002, when it was discovered that substandard working conditions and evidence contamination could have led to faulty DNA test results. Employees of the lab failed competency tests for DNA testing.<p>
Already, two men have been released after new testing was done on DNA evidence. Josiah Sutton was let go after testing exonerated him of rape. George Rodriguez was released after serving a 17-year sentence for rape, when it was found that he was convicted on "scientifically unfounded and inaccurate" testimony by a former crime lab employee.<p>
As the lab scandal unfolded, there were calls by criminal justice reform groups to halt executions in Texas while new DNA testing could be done on cases out of Harris County, which encompasses Houston. One-quarter of all Texas death row convictions have come from Harris County, and the crime lab investigation affects hundreds of death row cases, and thousands of criminal cases.<p>
The scandal grew worse several weeks ago, when lab investigators found 280 boxes of mislabeled evidence, including body parts, guns and other weapons. The evidence affects over 8,000 open and closed murder cases dating back to 1979. This revelation prompted the Houston police chief to call for a moratorium on executions of prisoners from Harris County. <p>
Meanwhile, just a few weeks ago, Texas death row inmate Ernest Willis was released from prison with some clothes and the standard $100--after serving 17 years for a crime that officials now say they aren't sure was ever committed. Willis was convicted of arson and murder, but now the authorities are saying that evidence suggests the fire was an accident. The prosecution in the case had withheld evidence and put Willis on powerful anti-psychotic drugs, which left him in a dazed state throughout the trial.<p>
The problems in the Houston crime lab are not isolated. All crime labs face backlogs and often have uncertified technicians. There is prosecutorial misconduct in many cases. There is racial bias. The indigent defense system is underfunded, and Texas is notorious for sleeping lawyers and otherwise incompetent capital defense attorneys. <p>
These latest problems should lead Texas officials to call for an end to executions, as well as a complete overhaul of the criminal justice system.<br>
<b>Lily Hughes, </b>Austin, Texas<p>

