<font face="Times New Roman, Times, serif" size="5"><b>Testing pesticides on poor children</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 12, 2004 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE BUSH administration and Corporate America want to use poor kids as guinea pigs to test the "safety" of pesticides and other chemicals.<p>
In an outrageous move, the Environmental Protection Agency (EPA) has proposed a two-year study--paid for in part by the American Chemical Council--of children's exposure to pesticides and household chemicals.<p>
The study will follow 60 children--from infants to age 3--in Duval County, Fla. In return for using pesticides in their homes, the EPA will give each family $970, some children's clothing and a camcorder that parents can keep.<p>
The offer is obviously designed to appeal to poor families--in a county where the rate of children living in poverty is already 20 percent higher than the national average. Likewise, Duval County's infant mortality rate is 10.2 for every 1,000 live births--as opposed to the national rate of 6.8. And while the death rate for per 100,000 children aged 10-14 is 19.1 in the U.S. population as a whole, it's a whopping 27.4 for Duval county.<p>
The plan for the new study is so outrageous that some EPA officials themselves are incensed. "It is important that EPA behaves ethically, consistently, and in a way that engenders public health," Suzanne Wuerthele, the EPA's regional toxicologist in Denver, wrote to colleagues in an e-mail obtained by the <i>Washington Post</i>. "Unless these issues are resolved, it is likely that all three goals will be compromised, and the agency's reputation will suffer." <p>
Troy Pierce, a scientist in the EPA's Atlanta-based pesticides section, wrote in a separate e-mail: "This does sound like it goes against everything we recommend at EPA concerning use of [pesticides] related to children. "Paying families in Florida to have their homes routinely treated with pesticides is very sad when we at EPA know that [pesticide management] should always be used to protect children."<p>
But EPA higher-ups continue to defend the study--saying it's crucial to study small children, because so little is known about how their bodies absorb harmful chemicals. Incredible! There are reams of scientific studies and evidence linking pesticide exposure to a host of health problems in both adults and children--including neurological and lung damage and birth defects. <p>
But the Bush administration and the pesticide and chemical industry--which continue to push for relaxation of regulations--don't seem to care.<p>
The companies of the American Chemical Council were exposed as corporate polluters earlier this year when a memo leaked from its PR firm outlined an elaborate plan of dirty tricks to undermine pioneering anti-pollution laws in California. Now, because Corporate America controls the purse strings, the EPA is helping them get away with using poor kids as guinea kids.<p>

