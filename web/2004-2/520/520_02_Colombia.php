<font face="Times New Roman, Times, serif" size="4">U.S. steps up military presence in an oil-rich region</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A war for oil in Colombia?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 12, 2004 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">COLOMBIA'S RIGHT-wing government is escalating its dirty war on leftist guerrillas to secure oil resources--and the U.S. government is more openly involved than ever.<p>
Officially, U.S. helicopters and surveillance are backing 18,000 Colombian troops as they target "narco-terrorists." "But the Washington-backed offensive has another motive, oil and military authorities say, one that Colombian and American officials only gingerly discuss: to make potentially oil-rich regions safe for exploration by private companies and the government-run oil company," the <i>New York Times </i>reported last month.<p>
The stepped-up U.S. military presence is centered on Putumayo, a remote region long targeted in the U.S.-funded fumigation program aimed at eradicating Colombia's coca crop. But Putumayo also happens to be at the center of the country's vast--but unexplored--oil resources.<p>
Thus, the Colombian military is functioning essentially as guards in Putomayo--for the country's state oil company, as well as Argosy Energy International of Houston, which operates 15 wells in the region. This is part of the government's turn towards allowing foreign oil companies, including U.S. giants ExxonMobil and ChevronTexaco, to increase their role in a country that ranks among the top 10 oil exporters to the U.S.<p>
Washington has welcomed these and other pro-business, free-market "reforms"--and the U.S. is backing the Putumayo operation by doubling the number of U.S. troops to 800, and increasing the number of U.S. citizens working for contractors from 400 to 600. Like the military personnel, the contractors will be involved in counterinsurgency operations.<p>
The offensive in Putumayo is more than a grab for oil resources, however. It represents the latest attempt by right-wing President Alvaro Uribe to consolidate his power.<p>
Uribe was elected to office in 2002 with a first-round majority--the first in Colombia's history--on a law-and-order program and a vow to win the country's long-running civil war. Since then, Uribe has tried to position himself as the U.S.-backed strongman of South America.<p>
But he has run into unexpected resistance--not only from the guerrillas, but also from the country's working class and urban poor. Uribe's effort to expand his powers with a 2003 referendum failed--and a left-wing former union leader, Luis Eduardo "Lucho" Garzon, won election as mayor of the capital city of Bogot&aacute;. Uribe next tried to pass legislation that would allow him to run for re-election, but this failed, too.<p>
Meanwhile, he escalated the government's long-running war with the Revolutionary Armed Forces of Colombia and the National Liberation Army by sending the army into rebel-held territories--with the enthusiastic backing of the Bush administration.<p>
He's tried to co-opt leaders of the right-wing paramilitaries in the United Self-Defense by offering them amnesty if they disarm--and inviting notorious paramilitary butchers to address Congress earlier this year. By winding down the paramilitaries and building up the regular armed forces, Uribe aims to better control--and legitimize--the terror tactics that the right has long used against union members and leaders of popular organizations.<p>
So far, this hasn't worked. A massive national strike and protest against government policies on October 12 highlighted widespread opposition to Uribe's failed free-market policies. As the Colombian newspaper <i>El Tiempo </i>put it, "What is certain is that the short-term future of the most popular president ever is not favorable."<p>

