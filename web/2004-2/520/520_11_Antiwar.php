<font face="Times New Roman, Times, serif" size="5"><b>"No blood for oil, U.S. off Iraqi soil"</b></font><p>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 12, 2004 | Page 11</font></p>

<font face="Times New Roman, Times, serif" size="3">WHILE THE mainstream media was still digesting election results, activists opposed to Bush and his war in Iraq took to the streets November 3 to show their opposition. And days later, as the U.S. ground assault on Falluja began, another round of protests took place across the U.S.<p>
In Chicago, the day after Election Day, about 1,000 protested under the banner, "Whoever wins, oppose the occupation of Iraq." <p>
In Austin, Texas, about 400 people met in front of the Capitol for an event that was also planned to show opposition to the war, no matter who won the White House. The mood of the crowd was reminiscent of a funeral, but became spirited and lively as it began to march with people chanting, "Don't attack Falluja!"<p>
By the weekend, as it became even more clear that the U.S. assault on Falluja was imminent, protests broke out across the country. Some of these protests were organized in response to national calls to action by groups like United for Peace and Justice, but some were the product of local organizing efforts.<p>
In Seattle, more than 1,000 people gathered downtown to hear speakers rail against the idea that Bush was trying to claim the election as a mandate for his agenda. Lt. John Oliveira, an inactive Navy reservist who served in both Afghanistan and Iraq, talked about the destabilization of Iraq and how the U.S. is not making the peace there.<p>
"No more blood for oil, U.S. off Iraqi soil," chanted the crowd, which impressed local activists with its size, especially considering that the event happened just days after the election. Not in Our Name called the protest, which was endorsed by International ANSWER and the American Friends Service Committee. <p>
In Los Angeles, more than 1,000 demonstrators marched through the streets of Hollywood to protest the occupation of Iraq, Palestine, Haiti and everywhere else that U.S. imperialism reaches. The overall message expressed by the speakers was the urgent need for a vocal and organized antiwar movement that collectively fights the escalating war in Iraq and elsewhere. <p>
Anger over the massive military spending that drains badly needed resources at home, concern over an impending draft and the continued discrimination against South Asian, Arab-American and other immigrants and people of color were also on the agenda. There was also a noticeable presence of high school students and young adults fed up with the system and alienated from the two-party duopoly that offers them little more than a future of war and economic uncertainty.<p>
In Madison, more than 800 people representing at least 13 different organizations came out to protest. "Don't mourn; organize!" implored Madison Area Peace Coalition organizer Mike Wunsch. After spending the last year campaigning for John Kerry, who only promised us more war and is now calling for unity behind our president, it's more important than ever for the antiwar movement to offer not unity, but opposition to their wars.<p>
In Montpelier, Vt., hundreds of activists from across the state marched to show their opposition to the war. "We're here to tell Mr. Bush that justice and peace are moral values," said Joseph Gainza, an organizer with the American Friends Service Committee.<p>
Nancy Brown, who joined Military Families Speak Out after her son was shipped out to Iraq last year, addressed the crowd. "Our troops are over there watching what we're doing," said Brown, pointing out that many have misgivings about the Bush administration. "Ours is a powerful voice," she said. "When injustice becomes law, resistance becomes duty."<p>
As <i>Socialist Worker </i>went to press, 300 people in Chicago again took to the streets as part of an emergency response to the assault on Falluja. The event--organized with 20 hours' notice--marched through downtown, chanting "Troops out now" and "No blood for oil," as passing taxis honked in support.<p>
The march ended by joining picketing strikers at the Congress Hotel, where one of the strikers addressed the crowd in Spanish. "We support your issues, and you support our issues," she said through a translator, as the crowd chanted, "No contract, no peace."<p>
<i>Glenn Allen, Josh Gryniewicz, Brandon Harris, Nathan Hensley, Katie Miller and Dan Troccoli contributed to this report.</i> <p>

