<font face="Times New Roman, Times, serif" size="5"><b>Cease-fire in Ivory Coast breaks down</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Chris Fagan</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 12, 2004 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE ALMOST two-year-old cease-fire in the Ivory Coast ended dramatically when government, rebel and French forces clashed repeatedly last week--leaving dozens of people dead and plunging the country back into civil war.<p>
In January 2003, a peace deal was brokered by France to end a four-month-long rebellion. The country was divided into a government-controlled southern region and a rebel-held northern region--with 6,000 United Nations (UN) peacekeepers and 4,000 French troops put in place.<p>
The recent violence came after the collapse of the unity government last month. Following a government air raid on rebel positions that killed nine French soldiers, the French attacked and destroyed government warplanes, precipitating a wave of demonstrations.<p>
France has long had its nose in Ivorian affairs. As the former colonial ruler in the region, it retains substantial economic and political influence in the country. During the late 1990s, the French government pressured the country to adopt International Monetary Fund-dictated "structural adjustment policies." The current rebellion is in part a consequence of those policies.<p>
The utter failure of France and the UN to bring about peace in the Ivory Coast is the latest in a long string of failures for outside intervention in Africa. Motivated more by a desire for oil and other natural wealth than any humanitarian concerns, the French are not a force for peace in the region.<p>
What will happen now to what the IMF touted as a "success story" in West Africa is uncertain. What is certain, though, is that peace can only come after an end to imperialist meddling.<p>

