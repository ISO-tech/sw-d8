<font face="Times New Roman, Times, serif" size="5"><b>Other Septembers and other Americas</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Elizabeth Terzakis</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 12, 2004 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Ariel Dorfman, <b><i>Other Septembers, Many Americas: Selected Provocations 1980-2004.</b> </i>Seven Stories Press, 2004, 252 pages, $14.95.</font><p>

<font face="Times New Roman, Times, serif" size="3">THE ESSAYS in Ariel Dorfman's <i>Other Septembers, Many Americas</i> span the history of imperialism in the Americas with elegant prose and unrelenting honesty. Chilean expatriate novelist Dorfman credits the events of September 11, 2001, with precipitating this collection of "provocations," but he is ever mindful of "other Septembers, other Americas"--namely the September 11, 1973, coup against the democratically elected president of Chile, Salvador Allende.<p>
In more than 40 essays, speeches, reviews and poems, Dorfman moves back and forth over history, drawing together apparently disparate events. September 11, the day that "we Americans" experienced "the terror and victimhood that so many other inhabitants of this scarred planet have had to wade through day after day since birth" is put into historical context. "Never before had Americans been asked to imagine themselves so deeply and distressingly a part of the rest of humanity."<p>
Dorfman--whose various exiles have bounced him from Argentina to Brooklyn to Chile and back to the U.S. after the Pinochet coup--offers a unique perspective on subjects from imperialism to video games. No one is left out of Dorfman's global vision, and no one escapes responsibility. Those not directly responsible for the current state of the world are responsible for recognizing its failings and acting to change them.<p>
In a commencement address at American University, Dorfman details the world as it is--"at this moment that I speak, a bomb is tearing apart somebody's father"--and then calls on his audience to commit themselves to making change: "What a privilege it is that you will soon find yourselves in a position to do something about these troubles...a complex and interesting life may indeed await us, as long as we wish to do something about the suffering of others."<p>
Writing for the <i>Los Angeles Times </i>in 2001, Dorfman seems to see ahead to the torture scandal of Abu Ghraib when he notes, "At the moment when the United States is demanding that Slobodan Milosevic be extradited to the Hague to face judgment for his possible participation in brutalities carried out by his troops, it would be the epitome of hypocrisy to overlook or not scrutinize similar offenses committed by the U.S. military." But the scandal he is discussing belongs in fact to the past, to recently revealed truths about U.S. actions in Vietnam.<p>
Unfortunately, Dorfman's anti-imperialism is not always consistent. While he rejects the atrocities committed in the name of the "war on terror," he accepts the logic of the Clinton administration's "humanitarian intervention" in Kosovo. And while he insists on activity from all, his suggestions about what form that action might take are more poetic than practical.<p>
Nevertheless, this book is well worth reading, both as primer and reminder of what states do in the name of progress and democracy.<p>

