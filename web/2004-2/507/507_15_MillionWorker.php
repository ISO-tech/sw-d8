<P><font face="Times New Roman, Times, serif" size="4">Organizing for Million Worker March steps up</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"We're mobilizing as workers"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 23, 2004 | Page 15</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE MILLION Worker March (MWM), set for October 17 in Washington, D.C., gained new momentum July when the National Education Association (NEA) voted to endorse. Launched by International Longshore and Warehouse Union (ILWU) Local 10, the MWM has achieved significant backing in the Northern California labor movement and from a range of union locals and labor councils around the U.S., including the South Carolina AFL-CIO.</P>
<P>Nationally, however, the AFL-CIO--to which the NEA is not affiliated--has issued a letter to all affiliated bodies urging them not to endorse the MWM. "While we may agree with many of the aims and issues of the March, the AFL-CIO is NOT a co-sponsor of this effort and we will not be devoting resources or energies toward mobilizing demonstrations this fall," the federation said in a statement. </P>
<P>"What we have learned is that the rank and file really wants this," said Clarence Thomas, former secretary-treasurer of ILWU Local 10 and co-chair of the MWM. "The rank and file understand the importance of organizing in our own name, and to demonstrate our solidarity while empowering ourselves by having a major demonstration before the elections."</P>
<P>But labor leaders are adamantly opposed. "Many of those who head labor unions are very much connected to the Democratic Party and, as such, don't want to see this kind of demonstration take place before the elections," Thomas said. "And one of the reasons why they don't is that they really don't want to mobilize workers. What they want is to follow the directions from the Democratic Party in terms of money, voter, registration, precinct walking and phone banking --and that's it.</P>
<P>"They don't want us to be putting any demands on Sen. [John] Kerry, or mobilize before the elections, or put forward our own agenda. You mean we have to give all our money to Kerry and not put forward an agenda for working people? The Democratic Party has not been addressing an agenda for workers and has not done so for many years."</P>
<P>While the MWM won't draw anything approaching a million workers, it's emerging as a rallying point for labor activists and officials who are attempting to rebuild a progressive current within the unions. One such group, U.S. Labor Against the War, has tabled the issue pending further discussion.</P>
<P>In the meantime, leading left-wing intellectuals and activists have signed on, including Noam Chomsky, Howard Zinn and Danny Glover. A range of socialist and left-wing organizations, including the ISO, have also endorsed.</P>
<P>The demands of the MWM include: a national living wage, protection and enhancement of Social Security, guaranteed pensions, the cancellation of all corporate "free" trade agreements, an end to privatization, for workers' right to organize and for a repeal of Taft Hartley and all anti-labor legislation, funding public education, rebuilding the decaying inner cities, progressive taxation, repeal of the USA PATRIOT Act, Anti-Terrorism Act, slashing the military budget, and more.</P>
<P><I>For more information about the Million Worker March, go to www.millionworkermarch.org.</I></P>

