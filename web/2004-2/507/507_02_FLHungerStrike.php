<P><font face="Times New Roman, Times, serif" size="5"><b>On hunger strike in a Florida prison</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 23, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IN A courageous effort to draw attention to terrible conditions in Florida prisons, death row inmate Ronald Clark staged a 9-day hunger strike that began July 1. Clark was trying to highlight issues of neglect and abuse on Florida's death row, including high temperatures (with no access to fans for prisoners), excessive use of force by guards and the indiscriminate use of chemical agents.</P>
<P>In a June letter to the Florida legislature announcing his hunger strike, Ronald wrote about "the inhumane mercilessness of captivating a human being in a 9-by-6, 54-square-foot cage to endure 90-plus degree temperatures, with the high humidity of Florida, with no air conditioning or fans.</P>
<P>"The stench of sweltering human bodies, men facing the imminent imperil of death, yet left in this cage, forced to endure subhuman conditions. Permitted no more than three showers a week. These are the conditions of Florida's death row in the 21st century. Conditions created by the Florida Department of Corrections, inhumane conditions accepted by a 'civilized society.'"</P>
<P>Ronald was moved to the prison infirmary on the sixth day of his prison strike, after reportedly throwing up blood. But prison officials initially refused to give his mother information about his condition. The following day, Ronald wrote to a friend that "I tried to get ice from the nurse yesterday; they refused to give me any, knowing I've been throwing up water."</P>
<P>Now that Ronald's hunger strike is over, he faces the possibility of retaliation from guards. On the last day of the hunger strike, guards reportedly removed fans from the hallway in front of other prisoners' cells--and told angry inmates to "Thank Mr. Clark."</P>
<P>As Ronald explained prior to beginning his hunger strike: "The [Florida Department of Corrections] runs this place like the Mafia, from the top all the way down to the bottom and back up. Nobody--these guards, [commanding officers]--nobody is accountable, and the ones that are accountable to probably learned how to 'correct' from places like Abu Ghraib."</P>

