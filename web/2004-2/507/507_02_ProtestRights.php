<P><font face="Times New Roman, Times, serif" size="4">NYPD can't pen in protests</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Judge rules for protesters' rights</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Laura Durkay</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 23, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IN A victory for protesters, the Manhattan Federal Court ruled July 19 that New York City Police may not use many of the crowd-control tactics that they employed during the massive February 15, 2003, antiwar protests. The court ruling prevents cops from using metal protest "pens" to fence in demonstrators, from performing generalized bag searches, and from blocking streets leading to a protest without providing alternate access routes.</P>
<P>"In the context of the tumult over demonstrations at the Republican National Convention, this is an historic victory for the right to protest," said Christopher Dunn of the New York Civil Liberties Union. However, activist groups are still battling with New York Mayor Michael Bloomberg and the NYPD to secure permits for protests against the convention.</P>
<P>A mobilizing meeting called by United for Peace and Justice (UFPJ) last week drew a young, multiracial crowd of 200. The antiwar group is planning what is expected to be one of the biggest demonstrations--a march on the Sunday before the convention under the slogan "The World Says No to the Bush Agenda." A meeting of the RNC Clearinghouse, a forum for coordinating more decentralized actions, drew 300 people at its height.</P>
<P>Police have issued about a dozen permits so far, but are still stalling on many others, including for UFPJ's antiwar rally. After weeks of negotiations, the police issued a permit for a march past Madison Square Garden, where the RNC will be held, but refused to give UFPJ a permit for a closing rally.</P>
<P>The organizers, who are planning for a turnout of at least 250,000 people, have argued that the only safe and convenient place for that many people to rally is in Central Park. The police have so far refused--claiming that a rally will ruin the grass!</P>
<P>The cops have offered UFPJ a rally spot on the West Side Highway, where protesters would be stuck under the broiling August sun. UFPJ held a press conference and rally on July 15 to demand a suitable rally site.</P>


