<P><font face="Times New Roman, Times, serif" size="5"><b>N.Y. officials out to silence Lawrence Hayes</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 23, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
New York State corrections authorities are continuing their years-long campaign to silence an anti-death penalty fighter. Former death row prisoner Lawrence Hayes, a founding member of the Campaign to End the Death Penalty (CEDP), was re-incarcerated this April on a flimsy parole violation charge. </P>
<P>Lawrence had been out of jail on parole, but was in a serious car accident in February. He missed a parole meeting while he was hospitalized--and for this, he was rearrested and is behind bars, facing a life sentence.</P>
<P>A former Black Panther, Lawrence was arrested at age 19 on a trumped-up charge of killing a police officer in Queens. He spent more than two years on New York's death row before his sentence was reduced to life when the U.S. Supreme Court abolished the death penalty in 1972. </P>
<P>After his release on parole in 1991, Lawrence became a community activist and helped found the CEDP in 1995. Because of Lawrence's political background and his record as an outspoken opponent of the death penalty and criminal injustice, he has been the target of New York's Department of Parole on several occasions. </P>
<P>In 1997, just days after appearing at a Brooklyn rally against New York's new death penalty law, Lawrence was charged with a parole violation and returned to jail for more than two years. In part due to pressure from a grassroots campaign launched by the CEDP, New York's attorney general ordered his release. Lawrence's re-incarceration is also a product of a vicious drive by Gov. George Pataki to abolish parole--in a state that has one of the largest prison populations in the country. </P>
<P>Lawrence recently appeared in the film <I>Deadline, </I>which features former Illinois Gov. George Ryan, who emptied the state's death row in January 2003. The film was an entry at this year's Sundance Film Festival and sends a loud and clear message that the death penalty machine is broken beyond repair. </P>
<P>The CEDP is working with the film's producers to put pressure on the courts for Lawrence's release. New York authorities have held Lawrence hostage to their law-and-order agenda for too long. He should be released now!<BR>
<B>Lee Wengraf, </B>New York City</P>

