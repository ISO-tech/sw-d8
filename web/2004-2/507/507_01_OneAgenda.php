<font face="Times New Roman, Times, serif" size="5"><b>Two candidates, one agenda</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 23, 2004 | Page 1</font></P>

<P><font face="Times New Roman, Times, serif" size="3">FOR MILLIONS of people, Election 2004 isn't about who to vote for, but who to vote against. They fear and despise George W. Bush--with good reason--and want to see him kicked out of the White House in November.</P>
<P>But they should pay careful attention this month when the Democratic National Convention in Boston anoints John Kerry as the man to beat Bush. Democratic Party officials and their corporate backers will spill plenty of champagne and crack open a lot of lobsters. They have a lot to celebrate.</P>
<P>For them, Kerry is a dream candidate. He's promised to continue Bush's "war on terror"-- no matter how long it takes or how many people have to die. He says he'll pursue "homeland security" just as ruthlessly as Bush. He's proposed handing out tax breaks for rich corporations and Wall Street, and he insists that he's "in touch" with "conservative values."</P>
<P>The truth is that if you vote against Bush in November, you will still get the Bush agenda. Kerry may dress it up in a different package and promote it with gentler language--and then again, he may not. Either way, the content will be the same: More war, more occupation, more budget cuts in programs working people need, more attacks on our unions.</P>
<P>Last week, Kerry attended the NAACP's convention and scored political points vowing to stand up for African Americans--by, for example, making sure that Bush and his brother Jeb don't disenfranchise tens of thousands of African Americans in Florida, as they did in 2000.</P>
<P>This ignores the fact that Senate Democrats, Kerry included, had the opportunity to oppose this racist injustice in 2000--and they turned the other cheek in the name of national "unity." Then, in the name of "unity," Kerry and his blow-dried partner John Edwards backed Bush's war on Iraq and his war on civil rights at home.</P>
<P>Now that there's an election, the Democrats tell us that it's time for another kind of "unity"--the kind where liberals and radicals set aside what they believe in, hold their noses and vote for the "lesser of two evils." We deserve better.</P>
<P>Instead of setting aside our opposition to war and corporate power--which both Democrats and Republicans stand for--it's time for activists to make our voices heard. And make them heard so loudly that Washington can't ignore them, no matter who sits in the White House.</P>

