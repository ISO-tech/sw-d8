<P><font face="Times New Roman, Times, serif" size="4">How did Bush's service records happen to disappear?</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Mystery of the missing records</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 23, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE BUSH administration can't seem to find anything. First, Iraq's non-existent "weapons of mass destruction." Now, Dubya's military service records.</P>
<P>During the Vietnam War, frat boy George W. Bush used his family's influence to jump to the head of a long waiting list for a cushy spot in the Texas Air National Guard. For months toward the end of his time in the Guard, Bush seems to have gone AWOL as well--skipping out on his "patriotic duty" to work on the Alabama congressional campaign of one of his daddy's buddies.</P>
<P>In 2000, a <I>Boston Globe </I>examination of Bush's records revealed that he was suspended from flight status in August 1972 for failing to take his annual flight physical. And in May 1973, Bush's two superior officers in Houston wrote that they could not perform his annual evaluation, because he had "not been observed at this unit" during the preceding 12 months.</P>
<P>When the charges that Bush skipped out on his military service resurfaced last year, the White House went ballistic--labeling the scandal politically motivated slander, and claiming that it could prove that Bush had completed his Guard duty in Alabama. But the best the White House could come up with was a few records showing that Bush had been paid by the military for six days in October and November 1972--and that he took a dental exam at an Alabama air base in January 1973.</P>
<P>Nobody could find the bulk of Bush's payroll records, or any other document proving that he had served out those missing months. Now, the White House has a novel new explanation for why. According to the Pentagon, there are no records for Bush's service at the time because they were "inadvertently destroyed."</P>
<P>In response to months-old Freedom of Information Act requests by news organizations, the Pentagon announced last week that payroll records for "numerous service members" were ruined by the Defense Finance and Accounting Service in 1996 and 1997 during a project to "salvage deteriorating microfilm." Surprise, surprise--it turns out that Dubya was unfortunate enough to be one of the service members whose records were mangled.</P>
<P>But the White House may not be able to weasel out of the scandal so easily. According to reporters, Bush's records should also be contained on microfiche at the Air Reserve Personnel Center in Denver.</P>
<P>But for some reason, the White House has refused to authorize the release of those records. And according to the Associated Press, Texas law requires separate recordkeeping for state National Guard service--which means that records proving Bush served during the time in question should exist on microfilm in Austin, Texas, as well.</P>
<P>The Associated Press requested the Texas records four months ago under the Freedom of Information Act--and sued the Pentagon last month after being stonewalled. As <I>Socialist Worker </I>went to press, AP was asking a judge to order the Texas records released--to prove once and for all if the self-proclaimed "war president" is really a chicken-hawk.</P>


