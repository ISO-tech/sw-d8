<P><font face="Times New Roman, Times, serif" size="4">Inside the control room at Al Jazeera news</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>What the U.S. media ignore</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Hadas Thier</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 23, 2004 | Page 11</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2"><B><I>Control Room, </B></I>a documentary by Jehane Noujaim.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">AL JAZEERA, an Arabic satellite television channel, was launched in 1996 as the first independent news channel in the Middle East. It has grown to be the region's most popular news channel, with an estimated 40 million viewers. And its commitment to uncensored reporting has earned it the ire of Arab and U.S. government officials alike. </P>
<P>Jehane Noujaim's documentary about Al Jazeera,<I> Control Room, </I>offers an inside glimpse into the war on Iraq from a perspective rarely seen in the U.S. media. The news station was branded "Osama bin Laden's mouthpiece" by the White House early on in the "war on terror" and was equally slammed by the U.S. media for its "lack of objectivity."</P>
<P>Showing footage of bloodied casualties and of U.S. troops rampaging through Iraqi towns, they claimed, was only "inciting the Arab streets." U.S. officials complained that Al Jazeera's footage of American prisoners of war broke the rules of the Geneva Convention.</P>
<P>In the film, an Al Jazeera reporter points out the hypocrisy of complaining about "rules of war" while prisoners are held at Guant&aacute;namo Bay indefinitely and captured Iraqi soldiers are paraded on U.S. TV. Of course it's even more laughable now that we know that the administration flushed the Geneva Convention rules down the toilet in its treatment of Afghan and Iraqi prisoners of war. When a reporter asks Bush how he expects American prisoners are being treated, he replies, "I expect they'll be treated humanely, just like we're treating prisoners that we've captured."</P>
<P>This is almost as tragically comic as when Defense Secretary Donald Rumsfeld--war hawk and liar extraordinaire--explains, "Al Jazeera has a pattern of playing propaganda over and over and over again. We have a responsibility to tell the truth... but we're dealing with people willing to lie to the world to make their case." Sooner or later, he assures us, "they get caught lying and they lose their credibility."</P>
<I><P>Control Room </I>challenges the U.S. propaganda, providing footage of what the war looks like on the ground through the lens of Al Jazeera reporters. But it also challenges the idea that any news is able to be objective. Rather than claim, as Fox News does, that they are "fair and balanced," Al Jazeera reporters admit that they are horrified by the war and that the war itself makes it impossible to stay neutral.</P>
<P>This became all too clear when air strikes against Al Jazeera's office in Baghdad killed Tariq Ayoub. When a producer is asked whether Al Jazeera reporters are "capable of being objective," she replies, "Are any U.S. journalists objective?....If there was true neutrality, there would be a welcoming of any information from any side."</P>
<P>During a summer of a growing crisis for the Bush administration in Iraq and thousands flooding to see Michael Moore's <I>Fahrenheit 9/11, Control Room </I>is a brilliant contribution to exposing Bush's war on the world.</P>


