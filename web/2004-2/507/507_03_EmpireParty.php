<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The other party of U.S. empire</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 23, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WHEN ONE of the U.S.'s two main parties gets in trouble overseas, the other is always there to bail it out--and the Iraq war and occupation is emerging as a classic example. With opposition to the Iraq occupation continuing to grow, Democratic presidential candidate John Kerry remains steadfast in his support for what is essentially Bush's policy.</P>
<P>Kerry's choice of a pro-war running mate, John Edwards, only reinforced this message. As Michael Klare, an expert on national security and defense politics, put it in a recent article, "Since September 11, 2001, there has arrived a new breed of Democrat: an enlightened hawk who seeks to neutralize the Bush administration's perceived advantage in security by advocating a more aggressive campaign against terrorism and nuclear proliferation. </P>
<P>"Prominent among the Democratic hawks is the presidential candidate John Kerry." Klare argues that Kerry's pro-war stance isn't simply an effort to woo Republican voters, but is part of a concerted effort by the party establishment to present itself as better qualified and more capable of pursuing an aggressive foreign policy.</P>
<P>"Like the Bush doctrine, it seeks to eliminate the threat of rogue states and terrorist groups," Klare wrote, "but it aims to do so by marshalling the collective strength of the world community, rather than that of the U.S. alone...The plan diverges from that of the Republicans. But it also endorses many of the administration's hard-line policies and in some cases is prepared to go further."</P>
<P>For Kerry, "going further" means adding another 40,000 troops to the armed forces and beefing up battlefield capabilities. "I don't fault George Bush for doing too much in the war on terror," Kerry said in February. "I believe he's done too little."</P>
<P>Kerry, in other words, is trying to outdo Bush by promising to carry out the occupation of Iraq--and future military interventions--more  effectively. He shares the same assumptions as Bush--that the U.S. is the world's cop and has the right to impose its will anytime and anywhere that Washington sees fit.</P>
<P>Nevertheless, many in the antiwar movement continue to support Kerry on the grounds that the Democrats overall must still be better than the Republicans on issues of war and peace. This view simply doesn't hold up.</P>
<P>Consider the recent speech by the Democratic candidate for the U.S. Senate from Illinois, Barack Obama, to the Chicago Council on Foreign Relations. An African American with a progressive record, Obama is one of the few prominent party candidates to generate enthusiasm among progressives.</P>
<P>Nevertheless, Obama toed the Kerry line on Iraq. "Although, I loudly and vigorously opposed the war in Iraq, I understand that it was an American commitment, not a Republican one," he said. "Now that we are there, all of us want to see the mission succeed. The stakes are enormous for the world and our own security."</P>
<P>Obama's plan for Iraq? Send <I>more </I>U.S. troops. This line was part of an overall endorsement of a tough U.S. foreign policy. Obama also went out of his way to attack Venezuelan President Hugo Ch&aacute;vez as a "demagogue" who "takes advantage of...economic inequalities."</P>
<P>This again dovetails with Kerry, who months ago declared that Venezuela, the top oil exporter to the U.S., was a "threat." After the failure of the U.S.-backed coup attempt in Venezuela in 2002, such comments will give political cover for the Bush administration to intensify its efforts to oust Ch&aacute;vez.</P>
<P>Far from Obama nudging the Democrats to the left, they've easily assimilated him--which is why he'll be giving the keynote speech at the Democratic National Convention this month. Democrats like Kerry and Obama are committed to winning the backing of Corporate America, the wealthy and the powerful--and that means maintaining U.S. imperialism's grip in Iraq.</P>


