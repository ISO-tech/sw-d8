<P><font face="Times New Roman, Times, serif" size="4">Senate defeats ban on gay marriage, but...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Democrats duck a fight</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 23, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE BUSH administration and its friends in the Christian Right suffered a defeat in their crusade to pass a federal ban on gay marriage in a Senate vote on July 14. In a 50-48 vote after three days of heated debate, supporters of the Federal Marriage Amendment failed to get the 60 votes needed to keep the measure alive.</P>
<P>In part, the measure's defeat demonstrates divisions among Republicans on whether amending the Constitution to ban gay marriage goes too far--with prominent GOP leaders like John McCain (R-Ariz.) and Lynne Cheney opposing the measure.</P>
<P>Other Republicans, however, appreciated the importance of making their Christian Right supporters happy during an election year, even if the amendment doesn't pass. "I would argue that the future of our country hangs in the balance because the future of marriage hangs in the balance," blustered antigay sicko Sen. Rick Santorum (R-Pa.). "Isn't that the ultimate homeland security--standing up and defending marriage?"</P>
<P>Democrats accused the Bush administration of introducing the amendment to distract attention from other election-year concerns, such as the crisis in Iraq and the U.S. economy. There may be some truth to this in terms of the Republicans' motives.</P>
<P>But gay marriage is <I>not </I>a "distraction"--and deserves to be taken seriously by politicians who say they are for defending gays and lesbians from right-wing attacks. Instead of taking the opportunity to stand up for equal rights, most Democrats used the debate to demonstrate how little they care about gays and lesbians.</P>
<P>Sen. Tom Daschle (D-S.D.) said that he opposed the amendment because it was "unnecessary"--since his state, South Dakota, already prohibits same-sex marriages. "In South Dakota, we've never had a same-sex marriage, and we won't have one," he said.</P>
<P>Three Democrats supported the amendment, and two senators didn't even bother to attend the vote--John Kerry and John Edwards. Next, the House of Representatives will take up the amendment, and it is expected that it will vote before the November election.</P>
<P>And at least 11 states will face ballot initiatives over the next three months over whether to write antigay discrimination into their state constitutions. If we are going to beat back the bigots and win the right to marry, gay rights supporters will have to organize protests telling the Democrats and Republicans that gay marriage isn't a "distraction"--it's a civil right.</P>


