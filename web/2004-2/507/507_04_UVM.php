<P><font face="Times New Roman, Times, serif" size="4">Mixed-up priorities at the University of Vermont</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>We're ready to fight back</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 23, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
When the faculty union at the University of Vermont negotiated its first contract, we won some of the best protections in the country for lecturers and adjuncts. No longer could these hard-working faculty members who make up a third of the university's full-time workforce be strung along on single-semester contracts--sometimes for 12 years and more!</P>
<P>Union members are learning, however, that a contract is only words on a page unless we step up to enforce it. Recently, that's just what we've done. </P>
<P>In an emergency meeting called by union delegates and department representatives, we urged our elected leadership to defend the job of a longtime lecturer and activist laid off by the university last winter. The lecturer, Dawn Saunders, has been a popular teacher of economics for the past eight years and was a leading activist in the drives to organize full-time and part-time faculty at the university. </P>
<P>As a member of the first negotiating team, she argued for language guaranteeing better working conditions and job security for lecturers, and an end to the university's widespread use and abuse of one-semester-only contracts. But when Saunders became the union's grievance officer, she found that one of the first jobs she would have to defend would be her own. </P>
<P>Though the administration could produce no evidence for its claim that Saunders' position wasn't protected by the contract, the conservative Vermont Labor Relations Board ruled last month against Saunders and United Academics.</P>
<P>Thanks to the rallying of members, United Academics will fight this attack. In the emergency meeting with the elected leadership, department representatives and delegates argued that this injury to Saunders was an injury to all--and a potentially fatal blow to the solidarity that's been created between tenure-track and adjunct faculty.</P>
<P>Though there's no guarantee of a favorable ruling, taking the case to the state Supreme Court demonstrates that United Academics will fight for all of its members, especially adjuncts most vulnerable to layoffs. But this fight must now deepen and spread. </P>
<P>At the same time the university has unveiled lavish new construction plans, including a new 10,000-seat arena, it is laying off extension faculty who provide valuable services to people all over the state in such areas as agriculture, natural resources, nutrition and youth education. </P>
<P>Members need to call on the union's officers to defend the jobs of extension faculty. We also need to increase our own activity by organizing a visible public campaign that can call out the administration for cutting faculty and let Vermonters know about the mixed-up priorities of its state university. <BR>
<B>Nancy Welch, </B>UVM United Academics, AFT/AAUP, Burlington, Vt.</P>


