<p><a name="Attacks"></a><font face="Times New Roman, Times, serif" size="4"><b>Stop attacks on immigrants</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Tamar Szmuilowicz</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Representatives from different community groups and local government officials gathered for a press conference July 15 to denounce the recent hate crime committed against Rajinder Singh Khalsa, a Sikh resident who was brutally attacked while walking in his neighborhood in Queens.</P>
<P>Singh Khalsa, known for his dedication to community service and volunteer efforts, was physically attacked by unknown perpetrators, who made remarks about his turban and made references to the September 11, 2001, attacks.</P>
<P>"We must denounce all forms of hate and bias," Partha Banerjee, one of the organizers for New Immigrant Community Empowerment (NICE), said. "This is not just about Sikhs, it is about all groups of people."</P>
<P>NICE organized more than 30 community groups to call on police and the district attorney's office to be more actively responsive against hate crimes. Several speakers pointed out, however, that the police are part of the problem since they carry out increased racial profiling, detentions and deportations of immigrants.</P>
<P>Monami Maulik from Desis Rising Up and Moving made this clear: "Hate crimes don't just happen in a vacuum. They are happening in a climate of anti-immigrant legislation." The press conference was followed by a July 17 community meeting organized by NICE that focused on trying to build the coalition against bias and hate. </P>

