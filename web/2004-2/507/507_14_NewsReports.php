<P><font face="Times New Roman, Times, serif" size="4"><b>Stop gay bashing</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Doug Kennedy and Kate Johnson</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEATTLE--About 250 people gathered in the heart of downtown Seattle July 11 to fight back against a vicious hate crime. Late on the Saturday night of Seattle's Pride weekend, three right-wing bigots jumped Micah Painter, a gay man, as he was walking home from the Timberline, a popular gay dance club.</P>
<P>One man slashed Micah with a broken bottle and opened up several large gashes in his back. "The whole time, they were yelling things like 'Die, faggot, die,'" Painter explained to <I>Seattle Gay News. </P>
</I><P>After a passing police cruiser failed to stop, Painter dragged himself back to the Timberline where friends called paramedics. At a local hospital, he was treated for cuts and bruises all over his body as well as three stab wounds, one of which caused internal bleeding. </P>
<P>Incredibly, another wounded man was wheeled up next to Micah--whom he believes to be his main attacker. But when Micah's partner tried to call the police, the officer kept cutting him off and saying that Micah himself had to provide the information.</P>
<P>By the time the cops finally showed up at the hospital--45 minutes later--the man was gone. To literally add insult to injury, the Seattle police and the local press dragged their feet before calling the attack a hate crime, a Class B felony that carries stronger penalties for the attackers. </P>
<P>Local groups called an emergency fundraiser and protest to offset Micah's hospital bills and send a message to right-wingers that we're ready to fight to defend ourselves.</P>

