<p><a name="NGA"></a><font face="Times New Roman, Times, serif" size="4"><b>National Governors Association</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Jorge Torres</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEATTLE--Fifteen hundred people converged around the Westin Hotel July 17 in protest of the National Governors Association. The group is one of the most influential lobbying organizations in the nation, according to <I>Fortune </I>magazine. It includes governors from the 50 states as well as U.S. territories and is responsible for the creation of a "Center for Best Practices" in education, health, social services, "workforce development" and homeland security.</P>
<P>This mostly "governors-only" meeting included Homeland Security Chief Tom Ridge, Health and Human Services Secretary Tommy Thompson and Contract on America architect Newt Gingrich. But it didn't take place without some angry people voicing their  frustration.</P>
<P>A midday student march from Seattle Central Community College to Westlake Center was met with as many as 15 police motorcycles, about 10 bicycles, four squad cars and two riot vans, which brought back memories of the police riot at the 1999 World Trade Organization meeting. Once at Westlake, a crowd made up predominantly of AFSCME members led a march sponsored by the Put People First coalition, which includes members of Jobs with Justice.</P>
<P>The rally, which also included Congressman Dennis Kucinich, called for an end to corporate greed, affordable health care for all and support for immigrant families. In the end, the solution presented by the speakers was to get out and vote Bush out of office. But it will take more than voting for a Democrat to beat back the National Governors Association.</P>


