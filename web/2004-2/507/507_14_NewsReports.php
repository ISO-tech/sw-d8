<p><a name="Marriage"></a><font face="Times New Roman, Times, serif" size="4"><b>Gay marriage now</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Leah Golshani</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--More than 100 people turned out for a rally against the Federal Marriage Amendment July 14 after the measure had died that day in the Senate. The message from the rally, which featured local leaders of the GLBT movement and a national figure, Chrissy Gephardt, the daughter of former presidential candidate Dick Gephardt, was clear: "The Democrats did the right thing and brought us a victory in the Senate, so get out and vote for more Democrats." </P>
<P>John Kerry's name was mentioned only once, but the message was to vote for someone who has a chance to beat George W. Bush in November. One spectator, Jeff of Bethesda, Md., held a "Vote Nader" sign and told <I>Socialist Worker,</I> "Kerry is the next Bush. He is supposedly better because he is a Democrat, but he is not speaking out against the war and he does not want to give gays the right to marry." </P>
<P>We need to continue our fight for gay marriage--not just by fighting to prevent our rights from being taken away in the proposed amendment, but for demanding equal rights for all.</P>

