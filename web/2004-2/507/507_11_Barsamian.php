<P><font face="Times New Roman, Times, serif" size="5"><b>Writers and activists speak for our side</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Michael Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 23, 2004 | Page 11</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">David Barsamian, <B><I>Louder Than Bombs: The Progressive Interviews,</B></I> South End Press, 2004, 244 pages, $16.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">WAR AND occupation. Giant tax cuts for the rich. Increasing inequality. The perils that face the world today are growing greater--and so is the need for ideas about what the world <I>could</I> look like and how to get there.</P>
<P>Enter alternative broadcaster David Barsamian's new book, <I>Louder Than Bombs</I>, a collection of 21 interviews he conducted over the past several years. Those interviewed range from stalwarts of the left--Howard Zinn, Tariq Ali, Noam Chomsky and the late Edward Said--to activists and writers who are less well-known but still biting in their critique of U.S. imperialism and the assault on workers and the poor.</P>
<P>One of the best interviews is with '60s radical-turned-prison abolitionist Angela Davis. Her detailing of the horrific treatment suffered by prisoners in the U.S. is especially poignant given the recent torture scandal at Iraq's Abu Ghraib prison. When Davis comments, "the vast expansion of the prison system...has reached critical proportions," she could easily be speaking of either the U.S. Army or the Department of Corrections.</P>
<P>Unfortunately, a few interviews seem a bit out of place next to such committed fighters for social justice. Ahmed Rashid, for instance, a Pakistani journalist and expert on the Taliban, lauds the U.S. for its invasion of Afghanistan in 2001--a crime that killed thousands of Afghanis and has subjected the entire country to a brutal occupation.</P>
<P>His few quarrels with the U.S. is that its invasion of Iraq could lead the U.S. to lose focus on Afghanistan. A serious look at how the U.S. combats terrorism around the world--from Afghanistan to Iraq to Latin America--would show that <I>less </I>U.S. focus on Afghanistan may be a welcome relief for the people there.</P>
<P>It's also too bad that some of the best people in the book, such as Tariq Ali, Edward Said and Howard Zinn, are restricted to so few pages. Overall, <I>Louder than Bombs</I> is a good introduction to the left and some of its luminaries. For those new to the left or activism, it is definitely worth a read.</P>


