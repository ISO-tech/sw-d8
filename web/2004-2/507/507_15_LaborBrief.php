<p><a name="LAS"></a><font face="Times New Roman, Times, serif" size="4"><b>Legal Aid Society</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Lucy Herschel, delegate, 1199/SEIU</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--The 1199/SEIU members at the Legal Aid Society won a temporary victory against layoffs in July. After a series of membership actions including walk-ins, a press conference and an after-work picket, management announced that they were postponing the layoffs, which were to go into effect June 30.</P>
<P>Legal Aid had first announced layoffs of 150 members of the Association of Legal Aid Attorneys (ALAA)/UAW 2325 and 50 members support staff members, represented by 1199/SIEU. Management claimed that large budget gaps necessitated the cuts.</P>
<P>After union members protested to both to City Council and Board members, Legal Aid first rescinded the layoffs for the majority of the attorney and postponed the layoffs for 1199 members. ALAA members voted to take concessions in exchange for having all their layoffs rescinded.</P>
<P>Some members objected, calling for the two unions to unite to save all jobs without concessions, and 25 percent of the attorneys voted against the givebacks. For 1199/SEIU members, the layoffs could still be imposed.</P>
<P>And the corporate restructuring firm Alvarez &amp; Marsal has been brought in to restructure Legal Aid. As Andrea Veras, a word processor targeted for layoff,  said: "We need to continue fighting. If we sit back and see what happens, then it will be too late."</P>

