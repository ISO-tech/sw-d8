<p><a name="LAT"></a><font face="Times New Roman, Times, serif" size="4"><b>Los Angeles teachers</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs, United Teachers Los Angeles</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">LOS ANGELES--As the traditional school year came to a close last month, LA's public school teachers, represented by United Teachers Los Angeles (UTLA), won a series of surprising victories on the heels of two unprecedented union mobilizations.</P>
<P>First, the Los Angeles Unified School District (LAUSD) backed down from threats to make school employees pay $60 million more for health care next year. Then, the school board approved an estimated $43 million in cuts to the district's $1.3 billion administrative bureaucracy. </P>
<P>This will free up desperately needed money for school-site expenditures. As if to underline that point, the board also proposed a budget that reverses a threatened $50 per child school spending cut. And board members backed down on threats to lay off hundreds of counselors, nurses, psychologists and social workers.</P>
<P>These victories cannot be separated from the rallies of hundreds of teachers and supporters outside school board meetings on May 25 and June 8. At these protests, teachers showed a determination to fight back against health care cuts and demanded cuts in bureaucracy in order to increase school funding.</P>
<P>Members of Progressive Educators for Action (PEAC), an opposition group of UTLA members who want our union to wage a more militant fight against the attacks we face, played a crucial role in initiating and building these mobilizations. Now PEAC is discussing strategies for next year's UTLA elections as an opportunity to advance its vision for a more combative, rank-and-file unionism.</P>
<P>With the $400 billion military budget and the continuing profit-driven crisis in health care, the defensive victories won by LA teachers this spring are only temporary. The lessons of this spring's mobilizations need to be discussed and generalized among UTLA's 47,000 members in order for us to be prepared for the larger battles looming ahead.</P>

