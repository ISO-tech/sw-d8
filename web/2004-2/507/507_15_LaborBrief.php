<p><a name="Portland"></a><font face="Times New Roman, Times, serif" size="4"><b>Freightliner</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul Dean</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">PORTLAND, Ore.--Workers at the Freightliner plant here voted by just three votes to accept a weak new contract. The other three unions involved also accepted the contract. Earlier, the workers, members of the International Association of Machinists, had overwhelmingly rejected a previous offer.</P>
<P>The close (369-366) vote reflects the anger of workers who gave up so much in the last concessionary contract to make the company profitable. Like many other workers around Portland who have been made to pay the price of high unemployment, Freightliner workers are still left behind. The company has always dangled the threat of closing the plant and moving operations to North and South Carolina over the heads of workers.</P>
<P>The contract gives workers $2.50 an hour more over three years, and 40 cents per hour in pension contributions. But angry workers point out that this only takes them to where they were three years ago.</P>
<P>When the result was announced, many booed--indicating there was a willingness of workers to fight back. This anger is something the union should have built on to win a decent contract.</P>


