<P><font face="Times New Roman, Times, serif" size="4"><b>Seattle grocery workers</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Darrin Hoop, UFCW 1105</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE CONTRACT covering 25,000 members of the United Food and Commercial Workers (UFCW) union in the Puget Sound region has been extended for the fifth time, to July 23. The five UFCW locals have been negotiating with Safeway, Kroger and Albertsons under the guidance of a federal mediator since July 6 over a contract originally set to expire on May 2.</P>
<P>Despite the fact that these companies lost an estimated $2.5 billion in profits because of the four-month strike in Southern California, their goal of severely weakening the union shows no signs of slowing down. The companies are demanding a 30 percent cut in health benefits, a 24 percent wage cut, and a two-tier contract that will significantly lower pay, benefits and pensions for new hires.</P>
<P>The money to maintain--if not increase--pay and benefits, is there. Profits for the companies have increased more than 325 percent over the past decade to about $7 billion. Despite this, the union has proposed at least $120 million in concessions, mainly on health care.</P>
<P>The two sides claim to have reached tentative agreements on the retail clerk's pensions and retiree's health and welfare plan--but the union refuses to make public the details. Instead of being kept in the dark, union members need to demand that not only any tentative agreements on parts of the contract, but minutes of each negotiation session be displayed on the local's Web site. This would ensure a full and democratic discussion and debate about the direction negotiations are going in.</P>
<P>Some members are planning on mobilizing for a "no" vote and are making "No two tier" buttons to pass out. Members should also demand the linking up of this contract with the one covering 19,000 members of UFCW Local 588 in Northern California, where the contract has been extended past its initial deadline of July 17. </P>
<P>Neither set of workers should settle until both contracts have been settled. It will be up to the rank and file to lead the union forward.</P>

