<P><font face="Times New Roman, Times, serif" size="4">SF hotel workers want two-year deal and no copays</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"They make money, but we're the nuts and bolts"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Adrienne Johnstone and Kirya Traber</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 20, 2004 | Page 15</font></P>

<P><font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Chanting "Contract now!" 700 members of UNITE HERE Local 2 rallied in front of the Four Seasons hotel to demand a decent contract for hotel workers throughout the city. Their contract, which covers 7,000 workers at more than 60 hotels, expired August 14. </P>
<P>Organizers insist that there will be no extension of the current contract. Aurolyn Rush, a shop steward at the Grand Hyatt, called the hotels' initial contract offer "totally and utterly ridiculous." </P>
<P>In line with a plan leaked by the hotel corporations to the <I>San Francisco Business Journal, </I>the bosses are seeking to drastically alter employee health benefits. If the hotels get their way, fees for insurance will rise from $10 a month to at least $250. And workers will have to take on more shifts to be eligible for health benefits. </P>
<P>Like bosses in other industries, hotel managers are crying poverty. In fact, the "Big Three" hotel corporations (Hilton, Starwood and Marriott) reported profits of $1 billion in 2003--and expect even more in 2004. </P>
<P>Profits are up by $400 million in the last three years--showing that despite recession and layoffs, hotel executives are doing just fine. "They don't have to worry about taking care of their families," said Jennifer, who was at the rally with her 10-year-old daughter. "The hotels are making a lot of money, but the workers are the nuts and bolts of the industry."</P>
<P>The hotels are also seeking to block the union's central demand--a two-year contract that would give San Francisco hotel workers a common expiration date with UNITE HERE members in Washington, D.C., Boston, Chicago and other major cities. The two-year contract will "make us stronger because we'll be fighting together," Rush said. As Local 2 organizer Kelly Dugan said, "It's not an issue of whether we get it or not; we're getting a two-year contract."</P>
<P>In his speech, UNITE HERE International President John Wilhelm signaled the importance of this struggle and its potential to be a turning point for unions. "When you look back on the 2004 hotel fight, I predict you will look back and say the most important issue we faced was the fight to get the two-year contract," Wilhelm said.</P>
<P>The rally was an important first step in building support for the hotel workers' contract campaign. The hotel bosses are organized and ready to fight, so it will be up to rank-and-file workers to organize their coworkers for rallies and committee meetings to challenge these corporations. </P>


