<P><font face="Times New Roman, Times, serif" size="5"><b>Election victory for a bigot in Tennessee</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Scott Johnson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 20, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"OBVIOUSLY, THESE ideas would be described as racist by anybody in 2004 America." That is James Hart, winner of a Republican primary for a Congressional seat in Tennessee--describing his <I>own </I>views.</P>
<P>Among those views--supporting "favored races" and eliminating "poverty genes." Hart's "end goal," he says, is to keep the United States from becoming "one big Detroit." While campaigning door to door, Hart often wears a bulletproof vest and carries a handgun.</P>
<P>He is an open supporter of eugenics, the long discredited pseudoscience that sought to eliminate the "bad genes" of the poor and people of color by limiting immigration and forcing women to undergo sterilization. The rise of eugenics in the early 20th century led to sharply curtailed immigration in the U.S. and inspired Adolph Hitler's Holocaust against Jews in Nazi Germany.</P>
<P>Hart won the primary after the Republican Party failed to put up a challenger to 15-year Democratic incumbent John Tanner. Tennessee Republicans have tried to distance themselves from Hart and called on supporters not to vote for him.</P>
<P>But when urged by Democrats to thoroughly renounce Hart's views, Tennessee Republican leader Jeff Ward called the request "racial politics at its lowest common denominator." Hart is extremely unlikely to win. But comments like Ward's--and the two parties' record of stoking racism for political gain--have sown the seeds for a campaign like this one.</P>


