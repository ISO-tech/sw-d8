<P><font face="Times New Roman, Times, serif" size="4">Democrats block Nader from the California ballot</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Our fight is far from over</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 20, 2004 | Page 12</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I've just heard that Ralph Nader and Peter Camejo will more than likely be kept off the ballot in California. Despite the best efforts of hundreds of volunteers (and three professional petitioning firms!) we weren't able to collect the necessary number of signatures.</P>
<P>While this is certainly a setback, we had some big obstacles to overcome: the vilification of Nader by the Democratic Party, its liberal hangers-on and confused progressives like Michael Moore, Norman Solomon and Medea Benjamin. (And, of course, the backward electoral laws that stifle independents didn't help, either.)</P>
<P>However, all is not lost. We brought together hundreds of activists committed to peace and justice despite the Democrats' best efforts. And we learned some lessons--the Democratic Party will do <I>anything</I> to hold onto power, and that means lies and slander to block Nader. </P>
<P>As a write-in campaign, the number of votes we get will go way down. But there's more than one way to measure people's disgust with the two parties. We can keep making noise and ask the tough questions Kerry and Bush don't want to hear.</P>
<P>We can recruit into our ranks and organize the vast majority of people who are not represented by the two parties of war and big business. I'm willing to concede that we won't smash the two-party system this time around. But that doesn't mean we don't still have important work to do, inside and outside of the Nader-Camejo campaign. </P>
<P>Building a left-wing, independent political alternative means reinvigorating the labor movement, the civil rights movement, the peace movement, the women's rights movement and more. And that means a lot--a real lot--to millions of people suffocating for their rights and dignity as human beings in this country and abroad.</P>
<P>Not getting Nader on the ballot stings, but it is not the end of the world. Social movements, not just elections, will be our chariots of change. No matter what the result is in the next few days, I hope people will continue to join us over the next months and years as we continue to fight for Ralph, Peter and social justice.<BR>
<B>John Green, </B>Davis, Calif. </P>

