<P><font face="Times New Roman, Times, serif" size="4">Not your parents'<I>Manchurian Candidate</I></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A paranoid thriller for paranoid times</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Joe Allen</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 20, 2004 | Page 13</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2"><B><I>The Manchurian Candidate,</B></I> directed by Jonathan Demme, starring Denzel Washington, Meryl Streep and Liev Schreiber.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THIS IS not our parents' <I>Manchurian Candidate. </I>Unlike the 1962 Cold War classic that centered on a Moscow-Beijing conspiracy to seize control of the U.S. government, director Jonathan Demme's remake is a ferocious attack on the corporate domination of American presidential politics today. </P>
<I><P>The Manchurian Candidate</I> stars Denzel Washington, Meryl Streep and Liev Schreiber. It's based on the screenplay that director John Frankenheimer used for his 1962 film, which starred Frank Sinatra and Angela Landsbury.</P>
<P>It shocked many people for its story of mind control and political assassination. It's also a favorite for its hilarious depiction of a rabidly anti-communist congressman who whips up the threat of "communist control" to advance his political career.</P>
<P>Demme's film is set in the mythical present of a thinly disguised version of Bush's America. It is an America 15 years after the Gulf War and the recent "Indonesian incursion," which has resulted in a crackdown on civil liberties at home.</P>
<P>Much like the original, the film begins with a squad of American soldiers kidnapped and taken prisoner for three days (this time during the Gulf War.) While their patrol is reported "lost," they are subjected to the most advanced forms of psychological training and an implant is inserted into the brain of one of them--Sgt. Raymond Shaw.</P>
<P>The sinister figure behind all of this is not the Russian and Chinese communists of yesteryear but the evil Manchurian Global Corporation, a kind of super-sized version of Halliburton. It supplies Washington's war machine with all that it needs, raises private armies for wars and, oh yes, employs a renegade South African scientist whose experiments with mind control have made him an outlaw.</P>
<P>Shaw's "lost patrol" emerges from their detour to hell with all of them parroting the same story: They were attacked, lost and only found a way back because of Sgt. Raymond Shaw. Shaw is awarded the Congressional Medal of Honor and proclaimed a national hero.</P>
<P>Flash forward 15 years. Raymond Shaw has parlayed his "heroism" into a career as a liberal Democratic congressman from New York. But Manchurian Global has not forgotten about Ray Shaw and has big plans for him in league with his mother, Sen. Eleanor Shaw of Virginia, played by Meryl Streep. She is a domineering, scheming figure trying to maneuver her son into the vice-presidential spot on the Democratic ticket and eventually the presidency.</P>
<P>Manchurian's plans are disrupted by Major Bennett Marco, played by Denzel Washington, who was Shaw's commanding officer in Kuwait and recommended him for the Congressional Medal of Honor. Marco is tortured by dreams that Shaw's "heroics" in Kuwait never really happened and that something much more evil occurred.</P>
<P>How he unravels the conspiracy and the ending of the film I leave for you to see. Go to the movie and enjoy the paranoid conspiracy.</P>
<P>Sprinkled throughout the movie are references to the war on terrorism and the politicians' cynical manipulation of these events that will be familiar, such as a scene from a party convention when the nominees pose in front of a long line of smiling military personnel and firefighters.</P>
<P>The popularity of the <I>Manchurian Candidate</I> at the box office, along with <I>Fahrenheit 9/11 </I>and <I>The Corporation</I>, is another example of how large sections of the American population are deeply angry at the domination of mainstream politics by the rich and the reactionary politicians that serve them.</P>
<P>It is simply one of the best films of the year. After leaving the theater, I couldn't help wondering who is the <I>Manchurian Candidate</I> is this year's election. Bush? Cheney? Kerry? Edwards? They all are.</P>


