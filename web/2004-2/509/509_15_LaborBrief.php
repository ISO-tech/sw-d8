<P><font face="Times New Roman, Times, serif" size="4"><b>Seattle-area grocery workers</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Darrin Hoop, UFCW Local 1105</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEATTLE--Workers at Safeway, Kroger, Albertsons and several smaller area grocery stores in the Puget Sound region ratified a new three-year contract August 15. By an 83 percent "yes" vote, 20,000 members of United Food and Commercial Workers (UFCW) Locals 44, 81, 381 and 1105 ended the standoff that lasted three months past the contract's initial deadline. </P>
<P>UFCW Local 367 withdrew from the bargaining team a couple of weeks ago to negotiate on its own, saying that it didn't approve of the secrecy involved in the talks or the direction of negotiations. Union leaders claim that there are no two-tier agreements in the new deal, but this is at odds with details that emerged as <I>Socialist Worker </I>went to press.</P>
<P>For example, new hires will have a lesser HMO health plan for their first 35 months before qualifying for the regular plan. And new hires will take almost four years to reach journey pay, compared to the current two years.</P>
<P>For the first time, members must pay health-care premiums--$7 a week for individuals and $17 a week for families. Also, time-and-a-half pay on Sundays was cut to time-and-a-third. </P>
<P>Wage increases are only 30 cents an hour in the first and third year of the contract with a 30-cent bonus for those who stay through the second year. Some workers stand to lose more than $100 a month under this contract. </P>
<P>The Puget Sound deal is the latest in a string of concessionary contracts since the UFCW's defeat after a four-and-a-half-month strike by Southern California grocery workers earlier this year.</P>
<P>Still, despite only a couple of members campaigning against the contract, the 17 percent who voted "no" show that a significant minority is fed up with the direction the UFCW is headed. Reformers can base their hope for the future on that.</P>

