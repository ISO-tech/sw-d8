<p><a name="Macys"></a><font face="Times New Roman, Times, serif" size="4"><b>Macy's</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Marcia Thorndike, UFCW Local 101</b></font> </P>

<P><font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Union workers at two Macy's stores voted August 1 to accept management's "final offer," with few of their demands satisfied. The 40-cents-an-hour wage increase in the contract barely keeps pace with inflation and is offset by rising health care costs. Personal time-off days will be deducted from vacation days.</P>
<P>A contract that codifies harassment and intimidation saw little improvement. "It's a disappointment," said Jessica Fink, a Macy's cosmetic counter worker. "The company gave zero respect to its employees who make their profits. Unfortunately, because of people's economic condition with our very low wages, and because of low morale, we didn't vote to reject the contract and go on strike for a better deal."</P>
<P>The disappointing contract came despite a raucous 1,000-strong one-day strike, in which United Food and Commercial Workers Local 101 President Mike Borstel and others were arrested.</P>
<P>Rank and filers participated in a Contract Action Team to prepare for the fight, and a rank-and-file bargaining committee represented workers in negotiations. These are positive, democratic structures, but workers at these two unionized department stores in a sea of nonunion retailers needed broad-based solidarity action.</P>
<I><P>Editor's note: A previous article on the contract fight at Macy's mistakenly implied in a quotation that 95 percent of the stores' workforce was first-generation Chinese immigrants. This is true of one department. We regret the error.</I> </P>

