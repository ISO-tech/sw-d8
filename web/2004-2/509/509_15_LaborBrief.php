<p><a name="Pace"></a><font face="Times New Roman, Times, serif" size="4"><b>Pace University</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Chris Williams, NYSUT</b></font> </P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--After a struggle lasting just over a year, adjunct faculty at Pace University, a private university with campuses in downtown Manhattan and Westchester County, forced the administration to grant them the right to unionize.</P>
<P>Mirroring a national trend, Pace administrators have steadily increased the percentage of adjuncts until they are now a clear majority of teachers--with low pay, no health care or pension plan, and no office space or job security. Many adjuncts must work part time at three or four colleges to eke out a living.</P>
<P>With an 80 percent turnout, adjuncts voted by a 2-to-1 margin to join the New York State Union of Teachers (NYSUT). The success of the organizing drive, in the words of NYSUT organizer Daniel Esakoff, depended on running "a bottom-up campaign, meaning, there were adjunct faculty involved in the organizing from Day 1."</P>
<P>The mass organizing committee consisted of 70 adjuncts, whose visibility and openness gave confidence to other workers. "We built a committee of leaders," said Esakoff. "The leaders in turn went out and built a community around them. It was not a top-down campaign. It was never our intention to electioneer and win an election.</P>
<P>"It was always our intention to build an organization that would take some of the boss's power away. There is no magic pill or magic leaflet or magic e-mail blast that will convince everyone of the need for a union.</P>
<P>"It takes hard work, workers talking to workers, or adjunct faculty talking to adjunct faculty, one at a time, until enough are convinced and ready to act. That's a lesson for the labor movement at large."</P>
