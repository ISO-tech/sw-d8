<P><font face="Times New Roman, Times, serif" size="5"><b>Leaving teachers behind</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jeff Bale</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 20, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE MONEY is there to pay school administrators handsomely--but not teachers. That's the reality uncovered by a recent Educational Research Service study, which found that since 1993, average salaries for public school superintendents shot up impressively, by 12 percent after accounting for inflation. Principals, too, got a piece of the action, with an increase of 4 percent.</P>
<P>Teachers, on the other hand, actually saw their pay go down over the same period--by an average of 2 percent in real dollars. The study, published in <I>Education Week,</I> looked at pay changes in 527 public school districts. </P>
<P>The results confirm what most ordinary teachers already know--school districts cry broke when it comes to teacher pay and more resources for schools, but open the coffers for superintendents and the central office.</P>
<P>One case in point is in the nation's capital. The Washington, D.C., school system has bragged about giving teachers a (rare) 19 percent raise in the first three years since 2001. But this pay hike still doesn't bring salaries in line with surrounding districts.</P>
<P>Meanwhile, the D.C. school board and mayor are trying to hire a new superintendent--the third this year--by offering a pay and benefits package of more than $600,000 a year. So far, even that outrageous sum hasn't been enough to find a willing candidate.</P>
<P>These results mirror the reality of Corporate America, where CEOs have seen their compensation skyrocket while workers work harder for the same or even less money. So the next time your school board says there's no money for raises or more classroom resources, tell them to look no further than the superintendent's bank account. There'll be plenty there to go around.</P>


