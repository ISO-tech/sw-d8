<P><font face="Times New Roman, Times, serif" size="5"><b>Don't dismiss labor's New Unity Partnership</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">August 20, 2004 | Page 12</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
We were excited to see Lee Sustar's coverage of the UNITE HERE (UH) convention as part of <I>SW</I>'s ongoing look at the New Unity Partnership (NUP) ("Behind the UNITE HERE union merger," July 23). But while Lee is no doubt correct that a major and necessary labor revival will only come through a dynamic, militant rank-and-file movement from below, the question remains: What do unions do in the meantime?  </P>
<P>We believe that while the NUP split will not solve labor's problems, it could impact rank-and-file development. <I>SW</I> should therefore not dismiss the questions or contemplated solutions raised by the NUP solely because it is led by undemocratic bureaucrats.</P>
<P>Lee cites the UH and NUP's undemocratic structure and canned convention "discussion" as evidence that their program is a new saddle on the same tired donkey. Many unions are undemocratic, and most conventions--which mostly don't happen often enough--are carefully scripted shows aimed at "mobilizing members," most often this year to support John Kerry.   </P>
<P>John Wilhelm, Bruce Raynor, Andy Stern and the rest are bureaucrats who haven't broken from the strategy of partnership with the bosses and the bosses' party, and therefore the changes they are making to tackle the real crisis facing the labor movement are bureaucratic ones. But does the fact that the NUP comes from this swamp necessarily mean they will fail and fail completely?  </P>
<P>We recently met several longtime union militants from UNITE plants in Pennsylvania at a HERE hotel workers' rally here in D.C. Though the merger decision was made "from the top," these UNITE members were excited to be allying with another activist union, to be connected to more militants in the region and, more soberly, to be doing something to try to keep their union alive.</P>
<P>It's true that the strategy of support for the Democrats and "partnership" with bosses from the top has been suicidal. But it's also true that U.S. labor law is constructed to kill unions and that there currently is no broad working-class movement to effectively fight either the law or the employers from the bottom up.</P>
<P>At least the NUP is step one: admitting we have a problem. We think it would be wrong for socialists and rank-and-file activists to dismiss or abstain from that debate because of its inevitable and enormous faults, or because it does not take place amidst a rank-and-file upsurge.<BR>
<B>Ben Dalbey and Nihar Bhatt, </B>Washington, D.C.</P>

