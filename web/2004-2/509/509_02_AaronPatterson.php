<P><font face="Times New Roman, Times, serif" size="4">Pardoned death row prisoner arrested</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Targeted by the Feds</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alice Kim</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 20, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE LEADER of Chicago's infamous police torture ring--fired police Commander Jon Burge--has finally been ordered to answer questions about his conduct. But with Burge's appearance in court just weeks away, police and prosecutors are taking revenge--by arresting pardoned death row prisoner Aaron Patterson on drug and weapons charges.</P>
<P>Patterson, who has been an outspoken activist against police violence and the criminal injustice system since his January 2003 release, was arrested in early August. He is being held without bail following a five-month-long undercover sting operation by the U.S. attorney's office.</P>
<P>Prosecutors used extensive wiretapping of Patterson's phone conversations and an undercover government informant to gather "evidence" against Patterson. They claim that Patterson sold heroin and marijuana and tried to buy guns in an effort to become a "drug kingpin."</P>
<P>But there are clear signs of a frame-up in this case. According to the <I>Chicago Tribune, </I>drug charges against the federal informant--"who has a long rap sheet with convictions for theft and robbery"--were dropped in exchange for cooperation with the authorities. Patterson says that he knew he was dealing with an informant and planned to expose the scheme against them--but police confiscated the evidence that could prove this.</P>
<P>Patterson is one of dozens of victims of Burge and his detectives, who for decades systematically tortured African Americans suspects to extract "confessions" that were later used to convict them. On September 1, Burge is scheduled to give a deposition in two federal civil rights lawsuits filed by Patterson and Madison Hobley, another former death row prisoner pardoned by then-Illinois Gov. George Ryan.  </P>
<P>For the last three years, a special prosecutor has been investigating the torture claims of 108 African American men--many of whom are still behind bars. But Burge has already said that he plans to assert his Fifth Amendment right and will refuse to answer any questions posed in the deposition.</P>
<P>To "greet" Burge, activists are planning a protest on the day of his deposition. Patterson would have been one of those protesters, but the new charges against him seem to be part of a concerted campaign aimed at discrediting the anti-death penalty movement, which has successfully exposed the injustices of the criminal justice system. </P>
<P>Earlier this year, Leroy Orange--who was pardoned by Ryan along with Hobley and Patterson--was charged with selling cocaine to an undercover police officer. Now that the public knows about their record of brutalizing suspects and convicting innocent people, police and prosecutors obviously hope that arresting former death row prisoners will bolster their image. </P>
<P>Their pursuit of Patterson and Orange--two men who were tortured by police and then locked up and waiting to die for close to 20 years for crimes they didn't commit--exposes the vindictiveness of police and prosecutors.</P>


