<P><font face="Times New Roman, Times, serif" size="4"><b>What would Jesus do?</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">AT LEAST somebody's looking out for George W. Bush's spiritual health. Recently, Rev. Martin Luther Agnew implored an affluent Kennebunkport, Maine, congregation, including President Bush and his extended family, to dump their material possessions. </P>
<P>Preaching to a packed Episcopal church just down the road from the Bush family's seaside estate, guest minister Agnew told the flock--many of whom had driven to church in their jaguars and BMWs--that "Gated communities tend to keep out God's people." </P>
<P>But, he said, "Our material gifts do not have to be a wall...They can very well be a door. Jesus says, 'Sell your possessions and give alms. I'm convinced that what we keep owns us, and what we give away sets us free." </P>
<P>The entire Bush clan was gathered in Maine for the wedding of Florida Gov. Jeb Bush's son. No word if the sermon prompted Dubya to shed his multimillion-dollar Texas ranch, millions of dollars worth of U.S. Treasury notes or other assets. <BR>
--Associated Press, August 8, 2004 </P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Going for the gold...and the green</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">HUMANITY...TOLERANCE...corporate sponsorship? If you thought the Olympics were about high-minded idealism, guess again. Strict regulations recently published by Athens 2004 dictate that spectators may be refused admission to events if they are carrying food or drinks made by companies that did not see fit to sponsor the games. </P>
<P>Sweltering sports fans who seek refuge from the soaring temperatures with a soft drink other than one made by Coca-Cola will be told to leave the banned refreshment at the gates or be shut out. Even the wrong bottle of water could land spectators in trouble. Fans will be allowed into the Olympic complex if they are drinking Avra, a Greek mineral water owned by Coca-Cola, which paid $60 million for the privilege of being one of the main sponsors.</P>
<P>Stewards also have been trained to detect people who may be wearing merchandise from the sponsors' rivals in the hope of catching the eyes of television audiences. Those arousing suspicion will be required to wear their T-shirts inside out.</P>
<P>The restrictions are even harsher for the thousands of stewards and volunteers working at Athens 2004 who have been supplied with uniforms but no shoes. "We have to provide our own shoes and we were told that we shouldn't wear trainers with a bright logo from a sports brand which is not an official sponsor like Adidas," said one. </P>
<P>The main sponsors of the games have paid more than $1 billion in total for exclusive advertising rights and privileges, including the use of the Olympic logo under their brand names. All American medal winners are required to wear an Adidas outfit on the podium, according to the company's sponsorship contract with the U.S. Olympic Committee. </P>
<P>As Kostas Giannis, a Greek sports fan, said: "I don't see why, after all the money that Greek taxpayers will end up paying to host the games, McDonald's should dictate what I can eat in my own city."<BR>
--<I>The Halifax Herald</I>, August 8, 2004</P>

