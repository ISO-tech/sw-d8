<P><font face="Times New Roman, Times, serif" size="4">Rank and file says "no" to concessions in second contract vote</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>UAW rejects Cat's "final" offer</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sarah Macaraeg and Bob Quellos</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 20, 2004 | Page 15</font></P>

<P><font face="Times New Roman, Times, serif" size="3">PEORIA, Ill.--Some 9,200 United Auto Workers (UAW) members at four different Caterpillar factories in central Illinois voted "no" August 15 on the latest contract offer from the heavy equipment manufacturing giant. The rejection--by an overwhelming margin for the second time in four months--was no surprise, since the latest offer continues management's trend of demanding more concessions. </P>
<P>Caterpillar's latest offer aims to introduce a two-tier pay scale and increases in health insurance co-pays, deductibles and premiums. This offer is nothing but an insult from a company bent on stealing every dime possible from workers while it sits on top of $20 billion in revenue from last year--and the company expects sales to increase by 25 percent this year. </P>
<P>Retirees are the group with the most to lose. The company is targeting their pensions and benefits. Under the current contract, retiree health care coverage is free, but management wants to put this cost back on the former employees--a burden that will funnel an estimated $280 a month from their fixed pension payments into health care coverage. </P>
<P>The contract also contains a plan to extract concessions from supplemental workers. While offering full-time positions with benefits currently not available to part-timers, management wants to force a reduction in hourly pay from about $9 an hour to $7. One Peoria worker said she would be better off working at McDonald's--where the wages and benefits are better, and her workplace would be just around the corner.</P>
<P>The rejection of the latest contract opens up the possibility of a strike at the Illinois plants. Before their contract expired April 1, UAW members voted to authorize a strike by a 96 percent margin. But UAW officials have done nothing to prepare for a walkout--despite management's clear message that it is prepared to play hardball.</P>
<P>After the latest contract rejection, UAW officials once again offered to return to negotiations. The union leadership behaved shamefully during a years-long confrontation at Cat in the 1990s. In 1995, for example, the UAW cut off strike benefits to force a return to work after a 17-month walkout--rather than mount a challenge to management's scab operation.</P>
<P>Despite the defeats suffered by their union, workers at Cat want to draw the line against further concessions. As one UAW member's picket sign put it at a demonstration outside the vote in Peoria, "The last best final offer is the last best final insult. Vote NO!"</P>


