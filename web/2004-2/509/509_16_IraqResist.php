<P><font face="Times New Roman, Times, serif" size="4">Resistance grows across all of Iraq</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The revolt against Bush's occupation</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | August 20, 2004 | Page 16</font></P>

<P><font face="Times New Roman, Times, serif" size="3">GEORGE W. Bush's worst nightmare is unfolding in Iraq. As <I>Socialist Worker </I>went to press, U.S. troops in Najaf were engaged in a bloody standoff with resistance fighters belonging to militant Shiite cleric Moktada al-Sadr's Mahdi Army.</P>
<P>Washington's assault inspired rebellions in town after town throughout Iraq, with rebels staging attacks on U.S. troops and Iraqi police. The nationwide upheaval is putting enormous pressure on the puppet government of interim Iraqi Prime Minister Iyad Allawi.</P>
<P>The Bush administration had hoped that by putting an Iraqi face on the U.S. occupation with a fake handover of power to Allawi on June 28, it could quell growing resistance and bring a semblance of stability to Iraq before the U.S. presidential elections in November. But the U.S. drive to win a quick victory against al-Sadr by surrounding and killing his militia fighters in Najaf has backfired.</P>
<P>In mid-August, a national conference of 1,100 Iraqi delegates that was supposed to organize national elections in January descended into chaos as delegate after delegate denounced Allawi for collaborating with the U.S. assault on Najaf--site of the Imam Ali Mosque, the holiest shrine to Shiite Muslims.</P>
<P>Meanwhile, tens of thousands of protesters took to the streets of Baghdad, Kufa, Samawa, Nasiriya and other cities to express their anger at the U.S. assault. U.S. troops were dug in around the mosque as <I>SW</I> went to press, poised for an all-out assault on the fighters inside if they get the word.</P>
<P>But Washington faces huge risks if it launches a final offensive. Despite the history of tension between Iraq's Sunni and Shiite Muslim populations, large crowds in the Sunni town of Falluja--where U.S. troops were driven out by rebels in May--held a demonstration, chanting "Long live Sadr, Falluja stands by Najaf against America."</P>
<P>In Diwaniya, demonstrators at a large rally attacked and occupied the offices of Allawi's party, and called for his resignation. The demonstrations in support of Najaf spread outside Iraq--to Iran, Bahrain, Pakistan and Lebanon.</P>
<P>The crisis began when U.S. soldiers and forces from the Allawi government violated a two-month-old ceasefire agreement with Sadr's forces--by arresting Mahdi militia fighters and launching a raid on Sadr's home. U.S. forces then surrounded Najaf and began launching rockets from helicopters and jet fighters at suspected outposts of Sadr's militia.</P>
<P>The rain of death destroyed homes and killed untold numbers of civilians in a city of half a million people. When resistance fighters fled into the ancient Valley of Peace cemetery--another Shiite holy site--U.S. commanders ordered soldiers and tanks to pursue them, destroying graves and mausoleums along the way.</P>
<P>Even some U.S. soldiers expressed reluctance about the incursion into the cemetery. "You just know you're destroying that tomb," said Sgt. Hector Guzman, shaking his head. "It doesn't feel right sometimes."</P>
<P>For Shiites who suffered oppression for decades under the regime of Saddam Hussein, the U.S. attack on their holiest site is the last straw. "I myself welcomed the Americans when they threw out Saddam," says Amad Kamal, an unemployed auto mechanic. "I took pictures of myself with U.S. soldiers and brought my own horse to them if it could be of service. But now, I realize what is happening here in Iraq is because of the Americans."</P>


