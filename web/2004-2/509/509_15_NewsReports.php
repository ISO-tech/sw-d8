<p><a name="Bush"></a><font face="Times New Roman, Times, serif" size="4"><b>Protest Bush</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Bob Kosuth</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">DULUTH, Minn.--With four days' notice, local activists joined forces to organize a 1,500-person protest against George Bush, who stopped here for four hours on a swing through the upper Midwest in July. Opposition to the Iraq invasion was a major theme of the event, mentioned by local labor leaders as well as veterans, one of whom focused on the class inequality at the heart of the military.</P>
<P>In addition to the main event, smaller protests took place throughout the city. About 100 protesters converged in one of the skywalks leading to the auditorium where Bush was speaking, and several were arrested for refusing to move.</P>
<P>On the lighter side, some dressed in tuxedos as the Enron Seven or Billionaires for Bush and walked along the street where the Bushies entered. Creative signs included, "Stop mad cowboy disease," "Draft Bush's daughters," "Halliburton thanks you for supporting the war," "How many lives per gallon?" and "War begins with Dubya."</P>


