<p><a name="CMM"></a><font face="Times New Roman, Times, serif" size="4"><b>Chicano Moratorium Movement</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Evan Kornfeld</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">LOS ANGELES--On August 29, activists will commemorate the 34th anniversary of the Chicano Moratorium March, one of the most significant events in the history of the Chicano rights movement and the movement to stop the Vietnam War. Today, with Chicano soldiers fighting and dying in Iraq, there's an urgent need for community members to again march through East Los Angles to protest an unjust war. </P>
<P>In 1970, Chicanos, who made up 6 percent of the U.S. population, were 20 percent of the casualties in Vietnam. When the police saw that the march had attracted a massive crowd--between 20,000 and 40,000 people--they lashed out, arresting more than 200 and injuring hundreds more.</P>
<P>Three protesters were killed, including Ruben Salazar, a <I>Los Angeles Times </I>reporter whose columns were popular because they gave voice to the grievances of the Chicano community. Order had already returned to the streets when police fired a high-velocity tear gas canister into the restaurant where Salazar was drinking a beer, killing him instantly.</P>
<P>In memory of Ruben and all the other fighters for justice who were killed, injured and arrested by the police that day, we will again march on August 29. Join us.</P>

