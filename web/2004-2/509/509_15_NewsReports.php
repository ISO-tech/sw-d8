<P><font face="Times New Roman, Times, serif" size="4"><b>Marriage is a civil right</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Ann Coleman</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SEATTLE--Gay rights activists won a significant victory when the King County Superior Court ruled August 4 that gay couples deserved the right to marry. About 100 activists gathered with six couples and their attorneys from Lambda Legal and Northwest Women's Law Center to celebrate the decision.</P>
<P>The ruling challenges Washington state's defense of marriage act that defines marriage between one man and one woman, so the case now heads to the state Supreme Court. For Mala Nagarajan and Vega Subramaniam, who've been together for six years, the importance of the ruling is hard to miss.</P>
<P>The right to marry would have already had a significant impact on Vega's finances. "Had we been married, I would have had the right to collect unemployment because I moved to join my spouse," said Vega. And "for tax purposes we are considered legal strangers. If one of us dies, the other will have to pay inheritance tax on our house because we are not legal spouses."</P>

<P>-- <B>In San Francisco, </B>in response to the California Supreme Court decision voiding same-sex marriages that were granted earlier this year, 400 people rallied here. Speakers linked the struggle for gay marriage to the fight for immigrant rights and civil rights struggles of the 1960s.</P>
<P>That message was clear four days earlier, when the Gay Asian Pacific Alliance held a rally in support of same-sex marriage. About 350 people turned out for the rally, which was called in response to recent anti-marriage equality rallies in San Francisco and Alhambra.</P>
<P>"In the old days there used to be laws against Chinese families now there are laws against gay families. These laws have to change," said Zaji Gershon Lieberman, a 10-year-old daughter of lesbian parents.</P>

<B><P>-- In San Diego, </B>activists in Marriage Equality California (MECA) have seen their group grow greatly in size and importance in recent months. About 100 marched in the San Diego Pride Parade past a crowd of 200,000 with about 30 couples married in San Francisco earlier this year.  </P>
<P>On the night that the California Supreme Court decided to void the marriage licenses issued to same-sex couples, phone calls and e-mails went out to organize an emergency rally of 200. But the organizers of the event set the wrong tone by insisting that the only hope now is in the courtroom, undermining an event set for the next day where couples, supporters and the media were going to the County Clerk's office to demand new licenses.</P>
<I><P>Chuck Stemke and Kenny Annis contributed to this report.</I> </P>

