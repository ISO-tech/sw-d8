<font face="Times New Roman, Times, serif" size="5"><b>Campaign to End the Death Penalty</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Bridget Broderick</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 19, 2004 | Page 14</font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--"I stand before you as a voice for justice!" said Monique Matthews, as she stood next to her mother Pauline and her brother Ryan. <p>
The Matthews fought for seven years to free Ryan, an innocent 17-year-old, from Louisiana's death row. They continued the struggle by welcoming the audience of 120 at the Campaign to End the Death Penalty's (CEDP) fourth national convention. <p>
Ryan's family was one of many at the convention that had been victimized by the racism and inequality of the death penalty. Billy Moore spent 17 years on Georgia's death row before being released. Moore never denied his guilt, but Georgia released him after the victim's family spoke out against his execution. <p>
"I want people to understand capital punishment, how diabolical it is," he said. "Everybody has the ability to change. People didn't give up on me even when they knew I was guilty." Moore has spent the last 12 years speaking out and vowed to join forces with the CEDP to end the death penalty in all states.<p>
Moore's speech raised an ongoing debate in the abolitionist movement that made its way onto the convention floor--whether abolitionists should call for a sentence of life without the possibility of parole as an alternative to capital punishment. Many former Illinois death row inmates, whose sentences were converted to life without the possibility of parole by Gov. George Ryan, wrote to the convention describing life without parole as a "slow death sentence" that leaves prisoners without hope.<p>
After some debate, delegates passed a resolution that the CEDP would not advocate life without possibility of parole as an alternative to the death penalty.<p>
Activist and author Barbara Ransby, the convention's keynote speaker, addressed the civil rights movement of the 1960s and encouraged today's abolitionists not to shy away from making connections with other movements for justice. The current elections were not a reason to despair, she argued, but to organize broad movements--since social change has always depended on movements for justice more than on politicians. <p>
"This convention energizes me," said Sandra Reed, whose son Rodney Reed is currently on Texas' death row. "I'm on a mission--there's no stopping us now! What do we want? Justice!"<p>

