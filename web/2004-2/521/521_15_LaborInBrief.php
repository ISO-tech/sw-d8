<p><a name="UCSC"></a><font face="Times New Roman, Times, serif" size="4"><b>University of California-Santa Cruz</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Cyndi Gacosta and Mariah Kornbluh </b></font><p>

<font face="Times New Roman, Times, serif" size="3">SANTA CRUZ, Calif.--More than 400 workers, teachers and students protested November 20 to demand a contract for workers at the University of California branch here.<p>
The workers, members of AFSCME, chanted, "What do we want? Union wage! When do we want it? Now!" and beat drums as they entered a campus conference center. "We are fighting for a wage increase, respect and opportunity to advance without favoritism," said union member Nick Gutierrez.<p>
AFSCME is seeking a wage increase in order to afford the cost of living in Santa Cruz, including health care and holidays. One AFSCME member in the custodial department doesn't earn enough to cover the costs of living in Santa Cruz, so she had to find a second job as a home caretaker for an elderly woman to be able to make ends meet. Another worker, Imelda Lopez, has been working as a custodian for more than 16 years. She began at $15,000 a year--but earns only $16,000 today, and has to pay $2,000 per year for parking on campus as well.<p>
The students on campus have threatened to shut down the UC-Santa Cruz campus if the university refuses to meet the demands of the workers.<p>
