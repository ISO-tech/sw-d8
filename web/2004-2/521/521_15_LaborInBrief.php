<font face="Times New Roman, Times, serif" size="4"><b>San Francisco hotels</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Adrienne Johnstone</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--The employers have stepped up their attacks on 4,000 locked-out San Francisco hotel workers by eliminating the workers' health insurance December 1. <p>
Hotel employer trustees on the San Francisco Health and Welfare Fund voted November 8 not to extend the health benefits of locked out workers--even though the fund has over $22 million in reserves and UNITE HERE Local 2 offered to contribute $4 million of its own money.<p>
This is continued punishment for a two-week strike by Local 2 that ended October 13. Since then, the bosses have attempted to starve Local 2 members into accepting deep concessions and abandoning the central demand for a two-year contract that coincides with union contracts in other cities. The hotels want to label hundreds of workers ineligible for benefits, while driving others off the company health care plan with prohibitive monthly costs.<p>
The mood on the picket line has turned somber. Jorge Bonilla, a banquet worker for 11 years at the San Francisco Hilton, told <i>Socialist Worker </i>that the demand for a two-year contract "in the beginning was okay, but now in this war, we need a new strategy to continue. The hotels don't care if we're here in January, but we can't [be]. We need a resolution this week."<p>
Many workers are looking to Mayor Gavin Newsom to provide an end to the lockout. Newsom has publicly sided with Local 2 and has cancelled the extra police cars assigned to monitor picket lines. Newsom has called on the hotels to allow workers to return to their jobs for a 90-day cooling off period--but hasn't taken a stand on the issues of this strike. <p>
Also, Local 2 leaders and the 125-member rank-and-file bargaining team have not effectively communicated to the members or involved them in planning their own struggle. Workers are fighting blind, not knowing the union's overall strategy. <p>
As the lockout enters its fifth week, rank and filers must take the initiative and do more than just hold the line in order to challenge the bosses. <p>
<i>Send contributions to the Local 2 Solidarity Fund, 209 Golden Gate Ave., San Francisco, CA 94102.</i> <p>
