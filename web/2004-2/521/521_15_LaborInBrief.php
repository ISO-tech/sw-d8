<p><a name="Rochester"></a><font face="Times New Roman, Times, serif" size="4"><b>University of Rochester</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jeff DeToro</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ROCHESTER, N.Y.-- About 400 University of Rochester employees and supporters showed up to picket and protest outside the university hospital in support of 1199 SEIU's fight for modest wage increases and continuation of current health benefits.<p>
The approximately 1,200 SEIU members--who have worked without a contract since October 30--rejected the university's offer of a paltry 2 percent wage increase. In addition, the university is trying to drive down the benefits of unionized workers to the same level as non-unionized workers in order to achieve "parity."<p>
Chanting slogans such as "two percent won't pay the rent" and "no contract, no work, no healthcare, no work," workers were defiant in their opposition to the university's offer. Union leaders vowed to do "whatever it takes" to maintain health care benefits that they've had for 25 years. Workers at the rally were greatly encouraged when a group of students came marching down the street with banners and signs in support.<p>
A federal mediator is set to meet with both sides to try and break the impasse. The workers' success will depend on their solidarity. Asked if the union could win this fight, one employee, Destiny, said, "If we all stick together as one, it can."<p>

