<font face="Times New Roman, Times, serif" size="4"><b>Hitting close to home</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THE LITTLE Egg Harbor Intermediate School was strafed with machine gun fire by an F-16 fighter jet at the beginning of November. The warplane was on military exercises from the Warren Grove bombing range and fired at least eight rounds from a 20-millimeter machine gun into the roof and parking lot of the school in Little Egg Harbor Township, N.J.<p>
Military officials said it was a matter of "pilot error." "The pilot was looking over his shoulder--he had no idea where those bullets went," explained Col. Brian Webster, commander of the 177th Fighter Wing of the New Jersey Air National Guard.<p>
That probably won't be much relief to parents of schoolchildren at Little Egg.<p>
Luckily, the incident happened at 9 p.m., when only a few people were left at the school. But according to residents, this is just the latest "friendly fire" incident due to military exercises. "We live near Exit 58 on the Garden State Parkway, and a couple of years ago, an F-16 crashed behind our house," said resident Denise Miller. "And a year or two before that, there was a fire from a bomb, and a second one, too. So this is the fourth incident in the last three or four years."<p>
The 9,400-acre government-owned bombing range, in the coastal New Jersey pinelands, has handled 3,700 sorties so far this year. It has been closed pending the outcome of the investigation.<p>
But according to the military, having jets accidentally shoot up your neighborhood may be the price of "freedom." "We all remember the days after 9/11, when all civilian aircraft were grounded, and the only aircraft in the air were planes like the F-16s, and many of my friends told me they were comforted by the sight and sound of those planes guarding our skies," said Gen. Maria Falca-Dodson, deputy adjutant general of the New Jersey Department of Veterans and Military Affairs. <br>
-- <b><i>New York Times</i>, </b>November 11, 2004<p>

<font face="Times New Roman, Times, serif" size="4"><b>Code Rubik's Cube</b></font><p>

<font face="Times New Roman, Times, serif" size="3">STEPHANIE COX, the owner of tiny Pufferbelly Toys in St. Helens, Ore., was surprised when she got the call from the U.S. Department of Homeland Security in July. "It's all very surreal, quite honestly," Cox told a reporter. "I thought it was a prank when I first heard. I couldn't understand why Homeland Security would be investigating a tiny toy store in St. Helens." <p>
A man identifying himself as a federal Homeland Security agent said he needed to talk to Cox at her store. When Cox asked what it was about, the agent informed her that "he was not at liberty to discuss that." "I was shaking in my shoes," said Cox. "My first thought was the government can shut your business down on a whim, in my opinion. If I'm closed even for a day, that would cause undue stress." <p>
Two agents showed up at the store in September. But it wasn't terrorists they were after.<p>
They informing a puzzled Cox that a toy she carried called the "Magic Cube" was an illegal copy of the popular Rubik's Cube. The agents ordered her to remove the Magic Cube from her shelves, and watched to make sure she complied. <p>
Turns out that they were wrong, of course. The patent for the Rubik's Cube had expired, and the Magic Cube didn't infringe on any trademark. <p>
But don't expect Homeland Security to apologize for scaring a toy storeowner out of her wits. According to spokesperson Virginia Kice, "One of the things that our agency is responsible for doing is protecting the integrity of the economy and our nation's financial systems, and obviously trademark infringement does have significant economic implications."<br>
-- <b><i>The Oregonian</i>, </b>October 28, 2004<p>

