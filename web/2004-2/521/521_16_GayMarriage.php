<font face="Times New Roman, Times, serif" size="4">Gay marriage is a civil right</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>We won't back down</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 19, 2004 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">THE BIGOTS in the Bush administration didn't waste a minute in broadcasting their plans to write anti-gay discrimination into the Constitution. Buoyed by his presidential victory and the passage of referendums banning same-sex marriage in 11 states on November 2, George W. Bush and his Christian Right buddies want to go for broke.<p>
Karl Rove, the shadowy guru of Bush's reelection campaign personally confirmed that a constitutional ban on gay marriage will come up during his bosses' second term. Bush supporters like James Dobson of the wing-nut "pro-family" group Focus on the Family are preparing to further their "moral values" crusade. At the top of their list of scapegoats are gays and lesbians who want the right to marry.<p>
Meanwhile, in the wake of the Republicans' so-called "moral values" victory, gay-rights activists are searching for answers.<p>
But the Democratic Party leadership is doing some scapegoating of its own. Many party leaders are blaming vocal gay rights supporters for going too far and losing the election for Kerry--or are targeting San Francisco Mayor Gavin Newsom for defying California law to issue marriage licenses to same-sex couples. This makes as much sense as blaming the civil rights movement for Ku Klux Klan attacks on it.<p>
Bill Clinton, who as president signed the anti-gay Defense of Marriage Act, said that Kerry's big mistake was not supporting the referendums to ban gay marriage. Actually, while Kerry never openly backed the bans, he did maintain that the "states should decide"--a line that could have been taken out of the segregationist history of the Jim Crow South.<p>
According to Jeff Trammel, co-chair of gay and lesbian outreach for Kerry, the key for Democrats is "figuring out how to talk about issues in a way where you're not for or against gay people," If the Kerry campaign is any indication, tailoring your message to the right doesn't make you more appealing. It only makes you more right wing.<p>
There's an opening to convince people why they should support gay marriage, if they don't already. After all, while exit polls showed that 22 percent of voters based their choice on "moral values," 35 percent of voters supported civil unions, and 27 percent supported gay marriage.<p>
Earlier this year, when thousands of lesbian and gay couples lined up at City Hall in San Francisco to collect their marriage licenses, the Christian Right were spinning in their pulpits. <p>
But they weren't the only ones watching. Millions of gays and lesbians and their supporters looked on with raised expectations about what was possible in the fight for equal rights. And others who may never have considered the issue before drew the conclusion that marriage is a civil right--and why should gays and lesbians accept anything less?<p>
We have to organize activism that takes on--and doesn't retreat from--a fight against the bigots.<p>

