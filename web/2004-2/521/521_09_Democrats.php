<font face="Times New Roman, Times, serif" size="5"><b>Why do the Democrats sell us out?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 19, 2004 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">FLIP-FLOPPER. That was the label that Republicans pasted on Democratic Party presidential candidate John Kerry at every step of the campaign. And it stuck--from the moment Kerry proclaimed that he voted for the $87 billion to fund Bush's war on Iraq "before he voted against it."<p>
Now, Democrats and their supporters are debating why Kerry blew it--and plenty are willing to concede that Kerry and his staff were sending mixed signals on many of the decisive issues of the campaign.<p>
But "flip flopping" isn't unique to John Kerry. It's an in-built feature of the Democratic Party.<p>
Contrary to its image of being the "party of the people," the Democrats, like the Republicans, represent first and foremost the interests of the rich and powerful. But unlike Republicans, Democrats get elected by pretending to share the concerns of their more liberal "base" of loyal supporters. This means that typical Democratic candidates have to speak out of both sides of their mouths as a matter of course.<p>
So, for example, the Democrats will pose as the party of peace if that is needed for electoral benefit--as was the case with Lyndon Johnson, who ran as the "peace candidate" against Republican Barry Goldwater in 1964. But once he won the election, Johnson dramatically escalated the U.S. war on Vietnam just a few months later.<p>
If today, the American ruling class' agenda is control of Iraq's oil resources and dictating the political landscape of the Middle East, then that's the Democrats' agenda, too. They may offer cosmetic differences in how to "get the job done" in Iraq--more troops, greater involvement from other countries, etc.--but the "job" is the same one as the Republicans.<p>
Fundamentally, the job of Democrats is to represent the interests of the ruling class--and convince ordinary people to vote against theirs. No effort to "bring back the soul" of the Democratic Party can change this fact.<p>
We need to build a political alternative independent of the Democratic Party. But most of all, we must organize struggles that reach beyond the narrow confines of electoral politics--which take up the fight to end war and racism, to win equal rights for gays and lesbians, including the right to marry, and to keep abortion safe and legal.<p>
With each struggle, there is the potential to change the political climate in this country--and put pressure on our "leaders," Democrat and Republican alike, to stop ignoring our agenda.<p>

