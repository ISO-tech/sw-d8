<font face="Times New Roman, Times, serif" size="5"><b>Destroying a city to show who's boss</b></font><p>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 19, 2004 | Page 3</font></p>

<font face="Times New Roman, Times, serif" size="3">THE SAME people who wanted you to believe in weapons of mass destruction and "Mission Accomplished" in Iraq are now trying to sell you a U.S. military "victory" in Falluja. And once more, the U.S. media are parroting their line.<p>
"Breaking a city in order to fix it," declared a headline in the <i>New York Times</i>--an echo of the notorious statement by a U.S. military officer in Vietnam that his forces had destroyed a village in order to "save" it.<p>
The horrific assault on Falluja was portrayed as a way to isolate an "insurgent stronghold." Instead, it only highlighted the strength of the Iraqi resistance movement as city after city saw new rebel offensives.<p>
The strength of the resistance--and the weakness of the government of U.S. stooge Iyad Allawi--was highlighted in Mosul, when resistance fighters took over most of the police stations, grabbing weapons and body armor. The U.S. military was so overstretched that insurgents took control of entire neighborhoods in Baghdad--under the nose of the U.S. occupation authority in its heavily fortified "Green Zone." Falluja, meanwhile, will tie down U.S. troops as resistance fighters re-launch guerilla operations.<p>
If the assault on Falluja has only multiplied military problems for the U.S., then why do it? Aside from reviving the pro-war propaganda machine in the U.S., the aim in Falluja was political--to smash opposition in the so-called Sunni triangle ahead of elections scheduled for January 31.<p>
By using heavily Shiite Muslim and Kurdish troops to attack a Sunni Muslim area, Washington aims to pit Iraq's long-oppressed Shiite majority against the Sunnis who dominated the Iraqi ruling class under Saddam Hussein and before--while manipulating the nationalism of the Kurdish minority in the north to suit the U.S.'s own needs.<p>
That's why the U.S. has given up on its attempt to smash the militia of Shiite cleric Moktada al-Sadr, brokering a deal in which Sadr's followers allied themselves with senior Shiite cleric Ayatollah Ali al-Sistani. Sistani, tellingly, kept silent as the U.S. flattened Falluja--and Sadr confined himself to a critical statement.<p>
Cementing this Shiite alliance is none other than Ahmad Chalabi, the one-time darling of the Defense Department who was dumped when he was accused of passing U.S. intelligence to Iran. Chalabi has resurfaced as a sectarian Shiite politician--allied with Sadr--who calls for self-determination for the Kurds and, by implication, Iraq's Shiite South.<p>
Such a carve-up of Iraq has been advocated by several influential voices in the Washington establishment. The U.S. barbarism in Falluja and the crackdown in Mosul--using troops from surrounding Iraqi Kurdistan--is a "a surefire recipe for civil war," as <i>Asia Times </i>writer Pepe Escobar put it.<p>
For now, however, the White House hopes that the election will help them reassert control by consolidating ethnic and religious divisions. This is to be achieved by holding votes for national party lists rather than individual candidates. For example, Sistani has assembled an all-Shiite list.<p>
Those in the antiwar movement who argue that the U.S. has the "responsibility" to remain in Iraq to "prevent" civil war should take another look. The truth is that the cloud of ethnic and religious bloodletting in Iraq has a silver lining for the U.S.--a pretext to support a strongman like Allawi or some other second-edition Saddam Hussein who is prepared to impose Washington's will. Thus, Allawi used the attack on Falluja to declare a 60-day state of emergency that drastically limits political activity in the run-up to the elections. <p>
That's the U.S. formula in a nutshell: In the interests of "democracy," launch a massive military attack; when the resistance counterattacks, restrict democratic rights to reinforce the power of an unelected government; and the puppets then engineer elections aimed at guaranteeing their grip on power--and the U.S. grip on Iraqi oil.<p>
The antiwar movement in the U.S., unfortunately, kept silent for months this year while supporting the pro-war candidacy of John Kerry. Now the attack on Falluja has once again exposed the horror of U.S. rule in Iraq--and underscored the urgency of the demand to bring the troops home now.<p>

