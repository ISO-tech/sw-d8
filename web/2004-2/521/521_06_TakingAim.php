<font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bush and the Christian Right on the rampage</b></font><p>

<font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 19, 2004 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">IN A November 6 speech, Democratic Party powerbroker Bill Clinton blamed John Kerry's defeat on liberal Democrats "for not engaging the Christian evangelical community in a serious discussion of what it would take to promote a real culture of life."<p>
While Democrats debate how much further to distance the party from a pro-choice position, the Christian right has gone on an anti-abortion offensive. Right-wing crackpot Jerry Falwell launched the Faith and Values Coalition last week, as a "21st century version of the Moral Majority."<p>
Even in its 1980s heyday, the Moral Majority was never anything close to a popular majority. The same is true today. Although Christian congregations ran the Republicans' get-out-the-vote machine this election year, the so-called "values voters" represented just a small minority of the electorate.<p>
But while liberal Democrats have been crying in their beer since the election, the emboldened forces of the Christian right have been demanding payback for Bush's victory. They want, among other things, immediate progress toward outlawing abortion--protesting pro-choice Republican Sen. Arlen Specter for predicting anti-abortion judges appointed by Bush are unlikely to survive a Senate filibuster.<p>
Although Specter has never opposed a Bush court nominee, and subsequently stated, "I have supported many pro-life nominees," protesters demanded that Specter be denied leadership of the Senate Judiciary Committee. "It sends the exact wrong message to the core of the Republican Party that helped win this election," sputtered Rev. Pat Mahoney of the Christian Defense Coalition.<p>
Bob Jones III, president of anti-interracial dating Bob Jones University, urged Bush to purge "moderate" Republicans from the White House. "If you have weaklings around you who do not share your biblical values, shed yourself of them," Jones warned Bush.<p>
Anti-choice pharmacists across the country are even taking a stand against the evils of birth control, refusing to fill prescriptions for oral contraceptives--including for rape victims. "I refuse to dispense a drug with a significant mechanism to stop human life," declared Karen Brauer, president of the 1,500-member Pharmacists for Life International. <p>
With less fanfare, Christian conservatives have been mobilizing local grassroots campaigns in communities such as Rockland County, N.Y., where Catholics oppose the county's plan to budget $19,070 for the local Planned Parenthood clinic. The fact that the clinic does not perform surgical abortions has not stood in the way of protesters. "These people are in opposition to Judeo-Christian principles," argued one angry opponent. "It's got to stop."<p>
In this context, it is easy to forget that a majority of Americans--including 43 percent of Republicans--still oppose overturning the legal right to abortion. More than a million pro-choice activists traveled to Washington, D.C., on April 25 to demonstrate in the "March for Women's Lives."<p>
But the march was a pep rally for the lukewarm pro-choice candidate, Democrat John Kerry--where activists were told to mobilize no further than the voting booth to defend abortion rights. Clinton's Secretary of State Madeline Albright--who oversaw the 1990s-era sanctions against Iraq that killed hundreds of thousands of Iraqi children--took a place of honor at the front of the march, despite her utter disregard for <i>Iraqi </i>women's lives.<p>
Now the lukewarm candidate has been defeated, and the pro-choice movement is nowhere to be seen as Christian evangelicals escalate the assault on abortion. The pro-choice movement's fealty to the Democrats, even as the party moved further and further to the right, meant abandoning any effective fight to defend women's right to control their own bodies.<p>
The key lesson from the women's movement that won legal abortion in 1973--long ago absorbed by the Christian right--is that grassroots activism is the only way to win, transcending reliance on politicians from either of the two ruling parties.<p>
The Christian Right's power is not in its numbers but in an overriding sense of purpose. Abortion rights can only be defended by returning to our proud principles, resurrecting the demand for "abortion without apology" and launching organizations that can mount a grass-roots counter offensive whenever Christian zealots mobilize--be it at the abortion clinic, the pharmacy or the Supreme Court.<p>

