<font face="Times New Roman, Times, serif" size="4">The nightmare in Falluja:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Why was our family bombed"</b></font><p>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 19, 2004 | Page 1</font></p>

<font face="Times New Roman, Times, serif" size="3">GEORGE W. BUSH and the war makers of Washington say that Falluja has been "liberated." So what does "liberation" look like?<p>
"There are bodies strewn in the streets, and most families were forced to bury the dead in their gardens," Hamid Flewa, a Falluja resident, told the BBC. "I can see lines of bodies alongside the pavement."<p>
Hamid is left asking why. "[W]e have no water or electricity," he said. "We are going through our food supplies very quickly. No more food can reach the city. We are human beings...Why should we have to go through this?"<p>
For Flewa and the other victims of the U.S. offensive, the horrible answer is that they are being used to send a message to the rest of Iraq--that anyone willing to stand up to the U.S. occupation will be crushed.<p>
What about the tens of thousands of innocent civilians caught in the path of U.S. bombs, mortars and bullets? Washington can't spare a thought for their lives. "Innocent civilians in that city have all the guidance they need as to how they can avoid getting into trouble," sneered Defense Secretary Donald Rumsfeld. <p>
Try telling that to Muna Salim--whose pregnant sister Artica and seven other family members were killed when two U.S. rockets struck their home. "I cannot get the image out of my mind of her fetus being blown out of her body," Muna told the <i>Scottish Sunday Herald</i>. "Why was our family bombed? There were never any fighters in our area." <p>
But the Bush gang doesn't care about such questions. They're only interested in "pacifying" Iraq in time for elections scheduled for January. That means unleashing the most deadly weapons in the Pentagon's arsenal--and whipping up U.S. soldiers to do the unthinkable.<p>
Like the U.S. Marine who was caught on videotape, killing a wounded, unarmed Iraqi prisoner in one of Falluja's mosques. NBC correspondent Kevin Sites said that several wounded prisoners had been left in the mosque to be picked up for medical treatment. But when a group of Marines entered and found them there, one saw a prisoner breathing. "He's fucking faking he's dead," shouted the Marine. <p>
"The Marine then raises his rifle and fires into the man's head," recounted Sites. "The pictures are too graphic for us to broadcast."<p>
It's horrors like this that explain why the U.S. may be able to win the battles with it overwhelming firepower, but it will the war--as growing numbers of ordinary Iraqis join the resistance out of hatred for the U.S. and its puppet government.<p>
Every day that the U.S. rules Iraq brings more atrocities, war crimes and the oppression of an entire people. We have to demand an end to the occupation now!<p>

