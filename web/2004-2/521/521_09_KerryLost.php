<font face="Times New Roman, Times, serif" size="5"><b>Yes, Kerry really lost</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 19, 2004 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">MUCH OF the Anybody-But-Bust left responded to John Kerry's defeat with despair about America's "turn to the right." But a minority sounded a different note--clinging to the belief that their man actually won, but the Bush gang managed to steal the White House again.<p>
The charges spread quickly in the days after the election--that voting machines were rigged, corrupt officials managed to fake results and so on. Journalist Greg Palast led the way with an article claiming that Republican fraud cost Kerry a victory in Ohio and New Mexico, which would have put him over the top in the Electoral College. Palast insisted that exit polls showing Kerry with a small, but solid lead in Ohio accurately reflected what voters thought they chose--but when spoiled ballots were thrown out from disproportionately Democratic areas, Bush came out on top.<p>
There is certainly evidence of shady goings-on on Election Day. Even more persuasive are allegations of Republicans stopping people from casting a ballot in the first place--as in Florida, where Gov. Jeb Bush helped out his big brother once again by ordering election officials to use a list of alleged former felons to be kept from voting that even its authors said was flawed.<p>
This is all further proof that the U.S. election system--supposedly a "beacon of democracy" to the whole world--is a mess.<p>
But even if the fraud was on such a scale in Ohio to cost Kerry that state--and thus, a majority in the Electoral College--the fact remains that George Bush won the popular vote by a 3.5 million vote margin, and no one is claiming Republicans stole that many votes. Claiming victory for Kerry on the basis of the undemocratic Electoral College--originally created to protect the interests of slaveowners, and perpetuated today to give sparsely populated, conservative states a bigger say in national politics--while admitting defeat in the popular vote, is a shady proposition for the left.<p>
The focus on vote fraud conspiracies deflects attention from a reality that is harder for Democrats to face. The simple fact is that many of the conservative "swing voters" Kerry and the Democrats sought so avidly were convinced--rightly--that Kerry wasn't a clear and principled alternative to Bush.<p>
However many votes the Republicans succeeded in stealing, John Kerry lost many times more by running a pathetic campaign that never seriously challenged the most beatable presidential incumbent in a generation.<p>

