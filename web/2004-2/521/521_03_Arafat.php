<font face="Times New Roman, Times, serif" size="5"><b>Behind the slandering of Yasser Arafat</b></font><p>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 19, 2004 | Page 3</font></p>

<font face="Times New Roman, Times, serif" size="3">YASSER ARAFAT'S death unleashed a torrent of venom from U.S. apologists for Israel's apartheid.<p>
"In a better world, the PLO chief would have met his end on a gallows, hanged for mass murder much as the Nazi chiefs were hanged at Nuremberg," wrote right-wing <i>Boston Globe </i>columnist Jeff Jacoby. The editorial board of the <i>Chicago Tribune </i>struck a similar chord. "He hoped that the killing of innocents in Israel would exert enough pressure for him to win with terror what he could not achieve through negotiation," the <i>Tribune </i>lectured.<p>
Welcome to the U.S. media's upside-down world, where the victims of Israel's state-sponsored terror who live under the yoke of a foreign occupation are invariably portrayed as "bloodthirsty," as "terrorists," as the "enemies of peace." Meanwhile, Israel's murderous military incursions, targeted assassinations, sieges and collective punishments of the Palestinian population are "legitimate" and "measured" responses.<p>
You'd never know that Israel has taken far more Palestinian lives since the Intifada began in 2000 than vice versa. Apparently, killing innocent civilians is legitimate--as long as the means employed are high-tech helicopter gunships firing missiles that destroy entire buildings, rather than the low-tech strategy of suicide bombers strapping explosives to their bodies.<p>
Still, perhaps the most ironic feature of the U.S. media's coverage were the universal pronouncements of Arafat's failure as a leader who left nothing behind for Palestinians but poverty, bitterness and despair. Such comments seemed to be in stark contast to the hundreds of thousands of Palestinians who poured into the streets to mourn Arafat's death. It was fittingly arrogant that American and Israeli journalists felt qualified to pass final judgment on Arafat's legacy--and though it has nothing to do with how most Palestinians feel, this judgment is taken as the final word on the matter.<p>
Arafat's embodiment of the Palestinian struggle for national liberation spanned decades, and his political role and popularity underwent numerous transformations. In 1993, when the Oslo "peace process" began, many Palestinians were hopeful that their dreams of statehood would finally come to fruition--and Arafat returned from exile as an elder statesman who had led his people toward the end of the long nightmare that began with their dispossession in 1948.<p>
But the "peace process" transformed Arafat from the leader of a liberation struggle into the leader of a police force, charged with maintaining Israel's security. Arafat was reduced to negotiating the Palestinians' terms of surrender--while Israel expanded its colonial settlements.<p>
Arafat's popularity sank as disappointment with Oslo crystallized. But when Israel declared in 2000 that he was no longer a suitable "peace partner," resentment at Israel's arrogance helped Arafat regain support--as Palestinians refused to let Israel humiliate a symbol of their struggle for self-determination.<p>
That struggle will continue--and it won't end until the creation of a democratic, secular Palestine with equal rights for Jews, Arabs and people of all religious faiths.<p>

