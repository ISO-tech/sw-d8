<font face="Times New Roman, Times, serif" size="4">Cutting through the moralizing about abortion</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Vera Drake is not a criminal</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 19, 2004 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Vera Drake, </b></i>written and directed by Mike Leigh, starring Imelda Staunton and Phil Davis.</font><p>

<font face="Times New Roman, Times, serif" size="3">MOVIES THAT even <i>mention </i>abortion are few and far between. So when a movie like <i>Vera Drake, </i>which depicts what life was like for women before abortion was legal, makes it to the theaters, it's a welcome surprise.<p>
The low-budget film by British director Mike Leigh tells the story of Vera Drake, a working-class woman in 1950s London who has secretly helped women end unwanted pregnancies for at least 20 years. Described as a "saint" by the people who know her, Vera busily moves in and out of others' lives, caring for the sick, feeding lonely neighbors and providing abortions to women in need.<p>
This film captures the daily hardships of working-class people in postwar London. In an early scene in the Drake's cramped apartment, Vera's friends and family discuss their shared experience of the Second World War--those who were killed on the battlefield or perished in their homes during the Blitz. For workers, wartime rationing never ended, so Vera buys black-market sugar and tea for her family.<p>
Their lives stand in stark contrast to the upper-class families whose homes Vera cleans--with their modern conveniences and hours of leisure time.<p>
This gap is especially pronounced when the film compares abortion options available to wealthy women and the women who turn to Vera. The distraught daughter of a family whose house Vera cleans obtains an abortion from a doctor for the right price, after telling a psychiatrist the magic words--that she'd rather kill herself than have the baby, the product of a rape. At the time, women who had enough money could obtain legal therapeutic abortions if they proved that they were suicidal.<p>
Vera's clients are almost exclusively poor and working class--young single women, immigrants, married women who already have more children than they can care for. When Vera's secret is revealed, her daughter's fianc&eacute;, who comes from a large, struggling family, puts Vera's mission perfectly: "If you can't feed them, you can't love them."<p>
In a standout performance, Imelda Staunton portrays Vera as a very ordinary woman, who does what she does because she knows that if she doesn't help, no one will.<p>
After she is arrested, you want her to stand defiant in the face of this injustice. But she doesn't--and this is part of the point. Vera is defying the law because that is the only option.<p>
In this way, the film cuts through all of the moral crap used by the Religious Right to vilify abortion. Abortion is not a question of morality, but about the basic needs of women. The strength of Vera's character is that she doesn't struggle with a moral dilemma--she simply feels that she is helping young girls who have nowhere else to turn. This idea--and Vera herself--fall apart when she's arrested and her secret is revealed.<p>
Women who sought illegal abortions usually didn't meet women like Vera. Poor and working-class women faced humiliating and dangerous conditions at the hands of unscrupulous back-alley abortionists. Vera may never have accepted a dime for "helping young girls," as she calls it, refusing to use the word "abortion," but most back-alley abortionists exacted a high price for procedures that weren't guaranteed to work and were more likely to mutilate or kill the women.<p>
The message is clear--making abortion illegal will not stop women from seeking it, and potentially dying in the process.<p>
Be prepared for many depressing and emotional scenes in <i>Vera Drake</i>--for this is a movie that takes on an important and virtually ignored subject, and it can't be done in any other way.<p>

