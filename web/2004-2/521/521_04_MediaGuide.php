<font face="Times New Roman, Times, serif" size="5"><b>A guide to the U.S. media's doublespeak</b></font><p>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 19, 2004 | Page 4</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>ANTHONY ARNOVE, editor of the book <i>Iraq Under Siege, </i>provides a guide for decoding the corporate media's doublespeak about the invasion of Falluja.</b><p>

CORPORATE JOURNALISTS in the United States are generally little more than stenographers for U.S. government propaganda. But in times of war--or alleged war--they tend to abandon stenography for cheerleading as their preferred mode of "reporting" the "news."<p>
Rather than independent analysis, journalists compete over who can give the most enthusiastic coverage for the home team. Reporting on the current bloody assault on the Iraqi city of Falluja--absurdly code-named Operation Phantom Fury by the Pentagon--provides a clear illustration. To help readers of <i>Socialist Worker</i> decode this coverage, here is a guide to some of the real meanings of the terms dominating the headlines.<p>
-- <b>Suspected terrorist hideout:</b> Any home destroyed by U.S. troops. <p>
-- <b>Terrorists:</b> Any of the 300,000 people who live in Falluja who are killed or who resist U.S. forces in any way.<p>
-- <b>Coalition: </b>The name for the U.S. military when it wants to portray its actions as having the support of other nations--for example, Poland. Poland has 2,500 troops in Iraq, though it has announced it will withdraw them in 2005. But, not to worry. Fiji has sent 170 troops to protect the U.S.-controlled Green Zone in Baghdad.<p>
-- <b>Rebel propaganda:</b> Any report of a civilian death or human rights abuse by the "Coalition," even if documented by human rights groups.<p>
-- <b>Propaganda center:</b> Hospital. This is the reason why the assault on Falluja began by seizing Falluja General Hospital. "Iraqi troops eagerly kicked the doors in," the <i>New York Times</i> reported, "some not waiting for the locks to break. Patients and hospital employees were rushed out of rooms by armed soldiers and ordered to sit or lie on the floor while troops tied their hands behind their backs." Why? Because, military spokesmen explained, the hospital was responsible for "inflated civilian casualty figures."<p>
-- <b>Preparing for elections: </b>Brutalizing the population. As Naomi Klein wrote in the <i>Guardian,</i> "Escape routes have been sealed off, homes are being demolished, and an emergency health clinic has been razed --all in the name of preparing the city for January elections. In a letter to United Nations Secretary General Kofi Annan, the U.S.-appointed Iraqi Prime Minister Iyad Allawi explained that the all-out attack was required 'to safeguard lives, elections and democracy in Iraq.'"<p>
-- <b>Body count: </b>An obsolete term as far the U.S. media are concerned. Estimates of the number of Iraqis killed in the assault on Falluja stand at 600, "although U.S. officials say an accurate figure is hard to obtain because of the use of air strikes and other heavy ordnance."<p>
-- <b>Terror Town: </b>Used originally to refer to Falluja, by the <i>New York Post.</i> But can be used to describe any city in Iraq with more than 200 people. In the week of the Falluja assault, the <i>Financial Times</i> notes, "guerrillas have struck in virtually every big city in central Iraq, including Mosul, Ramadi, Samarra, Tikrit, Baaqouba, Bayji and Baghdad."<p>
-- <b>Iraqi security forces: </b>Iraqis who will be left to do the dirty work once U.S. troops withdraw from their assault.<p>
-- <b>"Mission accomplished"</b>: The operation in Falluja has been "very successful," with "hundreds and hundreds" of insurgents killed or captured, according to chairman of the Joint Chiefs of Staff General Richard Myers. But, he added, "If anybody thinks that Falluja is going to be the end of the insurgency in Iraq, that was never the objective, never our intention, and even never our hope."<p>

