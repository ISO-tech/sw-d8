<font face="Times New Roman, Times, serif" size="5"><b>Is Bush afraid of a Bob Dylan song?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 19, 2004 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE SECRET Service showed up at Boulder High School in Colorado last week over concerns about a 40-year-old statement by Bob Dylan. The agency said it was investigating claims that George Bush's life was threatened at a rehearsal for a school talent show.<p>
The controversy started--where else?--on talk radio when the mother of a student claimed that her daughter had heard members of a band playing in the talent show say "George Bush, I hope you die, and I hope you die soon," and "I'll stand over your grave."<p>
Turns out that the band--which calls itself Coalition of the Willing--was playing a Bob Dylan song from the early 1960s called "Masters of War," a bitter protest against war and militarism. The final verse of the song--which is addressed generally to the politicians, generals and the arms makers who benefit from war--says: "And I hope that you die, and your death will come soon/I'll follow your casket in the pale afternoon/ I'll watch while you're lowered down to your death bed/And I'll stand over your grave 'til I'm sure that you're dead."<p>
Allysse Wojtanek-Watson, who sings with the band, told reporters: "We were just singing Bob Dylan's song...If you think it has to do with Bush, that's because you're drawing your own conclusions."<p>
Secret Service agents questioned the school's principal and a teacher who is the faculty adviser to a student group that organized an earlier anti-Bush protest--and then left with a copy of the Dylan lyrics to examine.<p>
Boulder High School had made the news a few days earlier when about 85 students occupied the school library overnight to protest Bush's election victory and speak out about their fears about the direction of the country. Students said they feared that Bush's reelection would lead to more wars overseas, worsening environmental conditions and a return of the draft. They asked to meet with state Democratic and Republican lawmakers to discuss these issues.<p>
Student Stephen Rostovsky told a reporter that he hoped the protest would "[let] people know that adolescents are there and saying, 'We're not going to take this. We want a voice, too. We want a place in this democracy .'"<p>
The protest had right-wingers fuming, both locally and nationally--especially about Jim Vacca, the faculty adviser to the group that staged the protest. Vacca told reporters that he was a member of the progressive Jewish organization Tikkun Community. "These kids are being used to promote an extreme leftist point of view on the taxpayers' dime," one right-winger ranted to a local radio station.<p>
Principal Ron Cabrera said he stands behind the students' right to make their voices heard. "I feel that the school and these students have been accused without being able to confront their accusers," Cabrera told a reporter, adding that no student or parent had talked to him about the allegations. "Why would someone do that?"<p>

