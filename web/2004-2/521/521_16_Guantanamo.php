<font face="Times New Roman, Times, serif" size="5"><b>Tribunals at Guant&aacute;namo ruled illegal</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 19, 2004 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">GEORGE W. BUSH may not get his way in withholding all rights from the prisoners of his "war on terror." Earlier this month, a federal judge ruled that military commissions set up by the Bush administration in Guant&aacute;namo Bay, Cuba, to try detainees from the wars in Iraq and Afghanistan are illegal under both the Constitution and the Geneva Convention. <p>
By arbitrarily classifying hundreds of detainees at Guant&aacute;namo as "enemy combatants," the Bush administration has tried to hold them in a legal limbo. The ruling brought a quick end to the ongoing trial of one detainee--and casts doubt on the Bush administration's plans for future ones.<p>
Justice Department spokesperson Mark Corallo complained to the <i>New York Times, </i>"By conferring protected legal status under the Geneva Conventions on members of al-Qaeda, the judge has put terrorism on the same legal footing as legitimate methods of waging war."<p>
But if anyone is being subjected to legal terrorism, it's the hundreds of detainees at Guant&aacute;namo. Earlier this month, for example, the <i>Times</i> reported that the military has been holding "combatant status review tribunals" for the prisoners at the U.S. gulag.<p>
These tribunals are the Bush administration's attempt to sidestep a June Supreme Court ruling that detainees have a right to challenge their detentions in federal court. With the status reviews, the Bush administration was hoping to find a legal loophole by rubberstamping hundreds of detainees as enemy combatants. <p>
The process itself is a sick joke--with detainees left to argue their cases for themselves, without assistance from lawyers, and forbidden from seeing the evidence the government is using to label them as terrorists. It's no surprise that of the 104 detainees the Pentagon has ruled on, 103 were found to have been "properly" deemed unlawful enemy combatants and "properly" detained.<p>
The new federal court ruling is a blow to the Bush administration's plans to keep any Guant&aacute;namo prisoner from seeing the light of day. "This ruling should put the final nail into the coffin of the military commissions," Jamie Fellner, director of the U.S. program at Human Rights Watch, said in a statement. "They should never have been created in the first place, and their implementation has been a disaster." <p>
