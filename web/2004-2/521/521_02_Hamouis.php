<font face="Times New Roman, Times, serif" size="4">Appeals court bars the Feds' attempt to deport them</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Hamouis win justice at last</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Darrin Hoop</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 19, 2004 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">AFTER NEARLY three years living under the threat of immediate deportation to Syria, a Seattle-area family has finally won justice. On November 8, the Ninth U.S. Circuit Court of Appeals ruled in favor of the Hamoui family, barring the federal government from kicking them out of the country--and sending them to Syria, where they would likely face political repression.<p>
The struggle to stop the U.S. from deporting Safouh, Hanan Ismail and Nadin Hamoui is an inspiring victory in the face of the Feds' post-September 11 witch-hunt of Arabs, Muslim and immigrants.<p>
The Hamouis' nightmare began on February 22, 2002, the first day of the Muslim holiday Eid. When Safouh answered pounding on his front door at 6 a.m., Immigration and Naturalization Service (INS) and FBI agents barged in with guns drawn, and began searching the house. <p>
Hanan Ismail, a mother of four, was awakened with a flashlight and a gun pointed in her face. Separated from her three other children--Rham, Mohamed and Sam--Hanan Ismail was imprisoned with her daughter Nadin for nine months. What's more, Hanan has Crohn's disease, a gastrointestinal condition that worsens with stress and an unhealthy diet. While in custody, she had to go to the emergency room 11 times--and was taken to the hospital in shackles at least five times.<p>
What was the Hamouis' crime? Hiring bad lawyers. The Hamouis moved to the U.S. from Syria in 1992 and have been trying to file for political asylum ever since. But several different lawyers failed to fill out asylum forms properly, leading the INS to decide that the Hamouis had illegally overstayed their visas. <p>
The family was arrested under John Ashcroft's Absconder Apprehension Initiative, which rounded up some 300,000 people faced with deportation orders. After September 11, the policy gave the green light for the racial profiling of anyone of Arab descent.<p>
After their arrest, community groups such as the Arab American Community Coalition (AACC) put pressure on the authorities by organizing weekly pickets outside the jail and getting the family's story out into the open. The three Hamouis were finally released at the end of 2002.<p>
And now, after another two years of legal wrangling, a federal appeals court has "found that the family suffered deficient lawyering from 1993-2002, and that the government should examine the evidence that the family will most probably be tortured, and possibly killed, in Syria," the AACC wrote in a press release.<p>
Though the threat of immediate deportation is over, the struggle will continue. The government could try to overturn this decision and restart deportation proceedings. Also, the three face a long process to gain permanent residency status. <p>
The family wants to continue telling their story to the public. "If it wasn't for the community support, the letters, the money, they wouldn't have been freed," said Rita Zawaideh of the AACC. "I think this is what helped the Hamouis."<p>
