<font face="Times New Roman, Times, serif" size="5"><b>NEIU faculty and staff get ready to strike</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By a UPI member</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 19, 2004 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--Members of the University Professionals of Illinois (UPI) have set a November 19 strike deadline at Northeastern Illinois University (NEIU). In response, NEIU Board of Trustees Chair Dan Goodwin released a letter claiming that the state of Illinois would not offer further funding for the university--and that students would have to pay more tuition should the faculty be given a raise.<p>
Goodwin failed to include the fact that the board--at the university president's request--had already voted to increase student tuition in the coming academic year. He also failed to point out that although the university budget has grown by 107 percent since 1995, the budget line for instruction grew by only 37 percent and budgeting for full-time faculty was up only 27 percent. <p>
During this period, student tuition increased by 70 percent--as did the salary of University President Salme Steinberg. She gets a salary of $210,000 and another $90,000 in perks, including a housing allowance and two cars.<p>
Then there are NEIU's bloated administrative costs, including large salaries and increasing layers of non-teaching assistant and associate deans as well as provosts and "special" positions. By contrast, our union is fighting for the quality of education at NEIU.<p>
The administration would like to pack a minimum of 45 students into every class, force faculty who already work on average 59.5 hours per week to increase our teaching loads by 25 percent, and push our salaries--which are already the lowest of all state universities in Illinois--even further behind. These demands neglect the students, staff, and faculty at NEIU and maintain administrative excesses. The union is circulating the budget realities, calling for a reallocation of funding to support instruction, rather than administration. <p>
Steinberg is taking a hard line, hiring a high-priced union-busting lawyer. We are preparing to strike on November 19--and expect that our organizing efforts of the past year and continued solidarity will carry us to victory.<p>

