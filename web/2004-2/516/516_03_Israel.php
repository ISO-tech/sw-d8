<font face="Times New Roman, Times, serif" size="5"><b>Israel buries the "peace" process</b></font><p>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 15, 2004 | Page 3</font></p>

<font face="Times New Roman, Times, serif" size="3">THE SUICIDE bombing that targeted Israeli tourists at an Egyptian resort hotel captured front-page headlines across the U.S. But the more lethal Israeli violence against Palestinians in the days before and after the hotel bombing barely earned a few paragraphs. <p>
This is the sick double standard of U.S. politicians and their media mouthpieces--an Israeli life means more to them than a Palestinian life. <p>
Israel's Operation Days of Penitence--the military's name for the latest round of killings and home demolitions in Gaza--has been one of the bloodiest episodes since the latest Palestinian uprising, or Intifada, began four years ago. Government officials claim they are "preventing further attacks on Israelis." But the real intent is to terrorize Palestinians who dare to resist the brutality of an apartheid state.<p>
Since the assault began at the end of September, more than 100 Palestinians have been killed and hundreds more injured. According to some reports, more than a third of the victims have been children. <p>
Israeli troops have used missile strikes, shootings and the bulldozing of homes, schools and mosques. "I saw two or three tanks and several bulldozers razing farmland near our house," Somaya Ibayd told an Al-Jazeera reporter. Several of Somaya's children were critically injured when Israeli forces bombed their neighborhood. "We took cover in the living room," Somaya said. "Then, out of nowhere, the tanks shells hit us...All I remember after that is seeing smoke."<p>
These horrific attacks come amid what Israeli Prime Minister Ariel Sharon claims to be the beginning of a "withdrawal" from Gaza. The fact is that Sharon's plan to unilaterally withdraw isn't about ending Israel's suffocating occupation. Israel will remain in control over all passage--by land, sea and air--of people and goods into and out of Gaza, essentially turning the tiny strip of land where 1.3 million Palestinians live into an immense prison colony. This will also free up Israeli forces for the even more aggressive grab of Palestinian land on the West Bank.<p>
This goal of intensifying the occupation by withdrawing from Gaza was confirmed in stunning fashion last week by Dov Weisglass, an adviser to Sharon. "The significance of the disengagement plan is the freezing of the peace process," Weisglass told Israel's <i>Ha'aretz </i>newspaper. "And when you freeze that process, you prevent the establishment of a Palestinian state, and you prevent a discussion on the refugees, the borders and Jerusalem.<p>
Weisglass even confirmed that unflinching support from the U.S. government played a direct role in Israel's calculations. "Effectively, this whole package called the Palestinian state, with all that it entails, has been removed indefinitely from our agenda," said Weisglass, "all with a presidential blessing and the ratification of both houses of Congress."<p>
As if to drive this point home, the U.S. last week vetoed a United Nations (UN) resolution that would have condemned the latest round of attacks against Palestinian civilians--the seventh time that the Bush administration used its UN veto to shield Israel. Meanwhile, during the vice presidential debates, Democrat John Edwards and Republican Dick Cheney engaged in a particularly sickening exchange over who is more supportive of the state of Israel.<p>
All of this is outrageous, but not surprising, given that the U.S. establishment sees Israel as its number one ally in its quest to dominate the Middle East. Despite occasional quibbles over the best method to accomplish it, the Republicans and Democrats are completely united in that goal.<p>
As long as the racist state of Israel continues its brutal, exclusionary regime, Palestinians will continue to struggle for justice, dignity and the return of their lands. The real terrorists in this conflict are Ariel Sharon, George Bush and John Kerry.<p>

