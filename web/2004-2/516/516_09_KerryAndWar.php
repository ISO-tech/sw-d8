<font face="Times New Roman, Times, serif" size="5"><b>Kerry's bid to win the "war on terror"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 15, 2004 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">PARTISANS OF John Kerry--most of them opponents of the war in Iraq--feel emboldened in the wake of George Bush's embarrassing performance in the September 30 debate. As the disaster in Iraq unfolds and Kerry sharpens his rhetoric against Bush, they are feeling more confident that Bush will be sent packing on November 2.<p>
Yet amidst their euphoria for a Kerry campaign that finally seems to be bludgeoning Bush with the issue of the Iraq war, they've failed to notice--or chosen not to see--that Kerry is actually rehabilitating the "war on terror," the Bush administration's central ideological prop.<p>
Kerry's main critique of Bush's invasion of Iraq is that it has diverted resources from the "real" war on terror: pursuing Osama Bin Laden in Afghanistan, shoring up "homeland security" and preventing "rogue" regimes like Iran and North Korea from obtaining weapons of mass destruction. By contrasting the "real" war on terror to Bush's botched war in Iraq, Kerry is using public distrust with the Iraq war to build support for an aggressive and expansionist U.S. foreign policy intended to enforce U.S. dominance in the world.<p>
On this goal, the U.S. establishment has been fairly clear for more than a decade since the Cold War ended. Yet it had a hard time convincing a skeptical public of the urgency of this project and the blood and money it would require. The September 11, 2001, attacks provided the opening it needed, and the "war on terror" became the ideological justification for a host of expansionist policies.<p>
On this, Kerry and the Democrats are just as committed as Bush and the Republicans. Even as they lambaste the Bush administration on Iraq, Kerry and running mate John Edwards sound more hawkish by the day. In the September 30 debate, Kerry attacked the White House's decision for not finishing the job when the Marines invaded Falluja last spring. <p>
Kerry-Edwards, the Democratic platform and new liberal darling, Illinois Sen.-to-be Barack Obama, warn ominously that Iran can't be allowed to obtain nuclear weapons. Obama, who appeared at many Chicago-area events opposing the war, is on record as advocating missile strikes against Iran. And despite his use of the Iraq disaster as a cudgel against Bush, Kerry still refuses to disavow his vote for the invasion in October 2002. Worse, he's increasingly talking about "victory" in Iraq--a fantasy that a President Kerry will find no easier to make reality than President Bush has yet.<p>
Still, what's most ominous about all of this isn't that Kerry and Edwards are hawks on the "war on terror." What's worse is that they have helped to make millions of people who hate the Iraq war and hate Bush into hawks.<p>
Defending Kerry against the baseless charges flung at him during the Republican convention, the <i>Progressive</i> magazine editorialized in October, "We need good intelligence, a prudent use of our military forces, and yes, the sensitivity to build alliances." <p>
Since when has it been the province of the left to advise the CIA, the Pentagon and State Department about how to better combat "terrorism?" Despite its brilliant exposure of Bush, Michael Moore's <i>Fahrenheit 9/11</i> also can be read as saying that the "real" war on terror should be fought in Saudi Arabia.<p>
If you're committed to the victory of the Democratic candidate, no matter how right wing, in November, it's easier to accept his politics as your own without even knowing it. And with an antiwar movement that has placed itself on hold for the election season, there's no large mainstream voice to expose the war on Iraq and the "war on terrorism" for what they really are--two pieces of U.S. imperialism's plan for the early 21st century.<p>
Right-wing <i>New York Times'</i> columnist William Safire's October 4 prop to Kerry, "Kerry, Newest Neo-con" should be a warning to antiwar activists: "[Kerry's] abandoned antiwar supporters celebrate the Kerry personality makeover. They shut their eyes to Kerry's hard-line, right-wing, unilateral, pre-election policy epiphany."<p>

