<font face="Times New Roman, Times, serif" size="4">Bosses take hard line after two-week UNITE HERE strike</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Battle is on at SF hotels</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Abraham Gomez and Gillian Russom</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 15, 2004 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Hotel workers here were set to end their strike October 13, but faced a probable indefinite lockout by employers.<p>
Some 1,400 workers, members of UNITE HERE Local 2, launched a two-week strike at four hotels September 29 in an effort to win better pay, protect health care coverage and win a common contract expiration date with workers in Washington, D.C., and Los Angeles, where workers have also authorized strikes. <p>
In response to the walkout, management at 10 other hotels locked out another 2,600 hotel workers--and the hotel owners' organization, the San Francisco Multi-Employer Group, has voted to impose a lockout when workers return October 13. Meanwhile, police have told picket captains that they would receive tickets if they allow workers to make noise late at night.<p>
Noise making was one way that workers asserted their confidence and kept the upbeat mood of the strike. Carlos, a striker at the Hilton San Francisco, was frustrated. "How can we win this if we can't even make noise?" he said.<p>
For its part, the media have covered little of what's at stake for workers, but have covered complaints about picket line noise by residents of Nob Hill, an upscale neighborhood where many hotels are located.<p>
Meanwhile, in Los Angeles, more than 1,000 hotel workers and their supporters gathered in front of the Hyatt Regency in downtown Los Angeles to demand a just contract and to show their solidarity with strikers in San Francisco. As Agustin Berber, a cook at the Biltmore Hotel for 29 years and a member of UNITE HERE Local 11, told <i>Socialist Worker</i>, "They [the hotel companies] don't want to give us anything--they want to keep it all. We want health care for our families. We want a two-year contract to unite us with the other cities in order to have more power."<p>
John Wilhelm, former HERE president and a top official in the newly merged union, took part in a pre-planned civil disobedience action in which 44 workers, union leaders and allies sat in an intersection. "These giant hotel corporations--they don't know that they've already lost," Wilhelm said in his speech. "They thought they could keep us separated in each city--but no, we are united.  We're here to show that we're willing to be arrested, and that we'll fight as long as it takes to get justice."<p>
As the workers awaited arrest, over 100 LAPD cops in riot gear moved in, pushing the crowd away from the street and threatening over a loudspeaker to use "non-lethal" weapons if those sitting in the street didn't disperse.<p>
Local 11 is launching a hunger strike and a boycott campaign to press for a just contract. But the hotel bosses are united and determined to resist the union's major demand for a two-year contract that would give them the power to strike in nine cities plus Hawaii in 2006. "We're all ready to go out on strike if it's necessary," said Berber.<p>

