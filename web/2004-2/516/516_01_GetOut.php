<font face="Times New Roman, Times, serif" size="5"><b>We say: Get out of Iraq now!</b></font><p>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 15, 2004 | Page 1</font></p>

<font face="Times New Roman, Times, serif" size="3">IT'S OFFICIAL...again. The Bush administration lied to get its war on Iraq.<p>
A 918-page report released last week by the U.S. government's top weapons inspector concludes that any stockpiles of "weapons of mass destruction" possessed by Iraq were destroyed long before last year's invasion--as early as 1991. Like the International Atomic Energy Agency, the CIA's own Iraq Survey Group--headed by Charles Duelfer, a Bush Sr. appointee who supported the war on Iraq--found no evidence that Iraq was trying to revive its nuclear program.<p>
John Kerry seized on the report's announcement to condemn George Bush for "misleading" the country into war. But Kerry and his fellow Democrats joined right in with the hysteria over Saddam Hussein's "weapons of mass destruction," paving the way for the bloody U.S. invasion. "If we wait for the [Iraq] danger to become clear, it could be too late," Sen. Joseph Biden (D-Del.)--now one of Kerry's favored advisers--warned in September 2002.<p>
Of course, the Duelfer report won't tell you that the weapons inspections process itself was a fraud, designed to fail. "The inspection process was rigged to create uncertainty regarding Iraq's WMD, which was used by the U.S. and the UK to bolster their case for war," former UN weapons inspector Scott Ritter wrote in the British <i>Independent </i>newspaper.<p>
None of this matters to the Bush administration--whose mantra is: What does it matter if we lied? As long as we got rid of Saddam Hussein, the world is a better place. In reality, what the Bush gang has done is legitimatize the policy of pre-emptive war--a new and even more terrifying class of terrorism. As Ritter concludes, "Those who embrace unilateral pre-emptive strikes in the name of democracy and freedom have produced results that pervert the concept of democracy, while bringing about the horrific tyranny of fear and oppression at the hands of those who posture as liberators."<p>
"Democracy and freedom"--this was the greatest lie of them all. As Bush claims "success" in Iraq, Iraq is without reliable electricity, sewage runs in the streets, and Iraqis endure the daily terror of living under U.S. occupation.<p>
In Samarra, a city that U.S. forces pummeled last week, Raad Rahim Ahmed doesn't see much "democracy." "What elections are you talking about?" he said, telling the <i>New York Times</i> that his wife and two children were killed by U.S. soldiers. "I've lost my entire family. Why should I trust this government? Why should I vote at all?"<p>
Kerry may condemn the way that the Bush administration went to war--"the wrong war, in the wrong place, at the wrong time"--but his plan for what to do next in Iraq is more of the same, and maybe worse. Now that the U.S. is there, Kerry says, we have to "finish the job." This means doing whatever it takes--more troops, more bombs, more misery.<p>
And while Kerry criticizes Bush's pre-emptive war on Iraq, he has no problem with pre-emptive war somewhere else. "If we have to get tough with Iran, believe me, we will get tough," Kerry vowed at the Cleveland debate with Bush last week.<p>
As long as U.S. troops remain in Iraq, any hopes of democracy are as imaginary as the claims of "weapons of mass destruction" that U.S. rulers used to get their war. That's why we say, "Troops out of Iraq now!"<p>

