<font face="Times New Roman, Times, serif" size="4">Rochester police target Puerto Rican Festival</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Under siege from police</b></font><p>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 15, 2004 | Page 4</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
As in many other cities across the U.S., in Rochester, N.Y., the annual Puerto Rican Festival is a rare opportunity for people from America's last colony to celebrate pride in their national identity. And just like in many other cities, the Rochester police seem to take special efforts to repress our community and keep Puerto Ricans "in their place."<p>
On August 15, when the annual Puerto Rican Festival ended, members of the community were coming home as they usually do, filled with the excitement of the festival. For the past 35 years, it has been the same: Puerto Ricans in a caravan of cars, waving Puerto Rican flags, blowing their horns, cheering and signaling the end of the festival. <p>
This year, the Rochester Police Department decided to blockade North Clinton Avenue and side streets with 150 police dressed in riot gear. It was like a neighborhood under siege. <p>
Using their horses and special vehicles, police arrested 27 people and issued 50 traffic summons. Some people, like Sonia Ruiz, were arrested right in front of their homes. Sonia is 64 years old and has resided in the Flamboyanes Housing Project for the past 29 years. <p>
An angry meeting with police representatives and community "leaders" called soon afterward by the mayor's office dissolved without accomplishing anything. Now, with the help of the ISO and the Spanish Action Coalition, we're organizing our own meeting to address the police repression and let ordinary people speak out about their experience. We need to stand up to the attacks on the Puerto Rican community.<br>
<b>Roberto Resto, </b>Rochester, N.Y.<p>

