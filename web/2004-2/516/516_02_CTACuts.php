<font face="Times New Roman, Times, serif" size="4">Chicago transit chief threatens service cuts and layoffs</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Making workers pay for the budget crisis</b></font><p>

<font face="Times New Roman, Times, serif" size="2"><b>By Adam Turl</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 15, 2004 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">"OH DEAR, tell me it ain't so. My husband sold our only car this summer and we planned to be relying on the [Chicago Transit Authority] more than ever." That was Henrietta Black's response to the announcement of a doomsday "gridlock" budget and service cuts by CTA President Frank Kruesi.<p>
The proposed budget would slash the country's second-largest transit system by one-fifth--including service reductions on 300 peak-period bus routes, the total elimination of 30 bus routes, cuts in overnight service on all trains and the layoffs of 1,250 CTA employees, mostly union bus drivers and train crews. Low-income workers on swing shifts and graveyard shifts will be especially hard hit, since many can't afford having a car, and overnight train service will be all but eliminated.<p>
Kruesi, a long-time stalwart in Mayor Richard Daley's Democratic Party machine, said that the 2005 CTA budget would make up an estimated $77 million shortfall. The CTA is asking for a bailout from the state of Illinois, but so far, the state government--which is entirely controlled by the same Democratic Party in which Kruesi is a leading figure--has promised nothing.<p>
This is the latest attempt to get working people in Chicago to foot the bill for the ever-growing wealth of the city's elite--through cutbacks in public services and attacks on unions. The announcement of the CTA cuts comes on the heels of the Chicago Public Schools' union-busting Renaissance 2010 Plan and a concessions contract offered to faculty and staff at the City Colleges of Chicago.<p>
For some things, however, there seems to be plenty of cash. Administrative costs more than doubled during the first five years of Kruesi's tenure at the CTA, which the <i>Chicago Tribune </i>called a "traditional enclave for City Hall patronage." According to NBC local news, 92 new management jobs were added at the CTA between 1998 and 2003. While many neighborhoods--especially on the South and West Sides, where the population is predominantly minority--have already had service cuts, CTA management moved into fancy new digs, at a cost of $93 million. <p>
And that isn't even to mention some of Daley's pet projects, such as the hugely over-budget Millennium Park. "Millions of dollars for Millennium park," one CTA rider, Damien James, told ABC News. "But what's more important? A tourist attraction or...people getting to their jobs?" For the Democrats who run Illinois, the priorities are clear.<p>
Even before the "gridlock" budget, the CTA was planning to make things tougher on the most vulnerable people who use public transit. Disabled passengers who rely on the CTA's paratransit service will see costs double to $150 a month. Ayonna Collins, who has cerebral palsy and uses the CTA to get from her home in the city to her job in the suburbs, told the <i>Chicago Tribune</i>, "Fixed income people...will be forced to choose between going to the doctor or to the grocery store."<p>
Kruesi says that he wants CTA riders to protest--and help him lobby Springfield. We should take him up on the protesting idea, but we shouldn't be fooled--Kruesi, Daley and their pals in the state house are all part of the problem.<p>
