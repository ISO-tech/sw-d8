<font face="Times New Roman, Times, serif" size="5"><b>Chicago's hollow monument to labor's struggle</b></font><p>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 15, 2004 | Page 4</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i><br>
It is a sad day in the history of labor when a symbol of revolution is used in a scheme for gentrification and tourist dollars. On September 14, a statue finally appeared on Haymarket Square in Chicago to honor the labor radicals who were executed a over century ago for advocating anarchism and revolution. <p>
On May 1, 1886, 80,000 workers marched through the streets of Chicago demanding an eight-hour workday. Two days later, police violently broke up a demonstration of striking workers, killing one worker and injuring several others. The next day, on May 4, thousands met at Haymarket Square to protest. Just as the demonstration was ending, a battalion of 175 police arrived. A bomb was thrown. The police opened fire. One police officer died immediately, as did four workers. Seven other officers died later of their wounds. <p>
History does not know who threw the bomb, but eight labor organizers were arrested. Five were sentenced to death, though the courts openly admitted they had no evidence, other than the fact that the men were anarchists. <p>
In 1890, May Day was celebrated as International Workers Day in honor of the murdered anarchists, and it has been celebrated ever since throughout the world. Only in the United States is the holiday largely ignored. For years, Haymarket Square stood empty, except for the cars that used it as a parking lot in this run-down industrial neighborhood. <p>
The unveiling of the new statue was an interesting event indeed. The moderator of the event was Chicago's commissioner of cultural affairs. After the moderator came the president of the Illinois state senate, and following him was the man who organized the artistic side of the project. <p>
The president of the Chicago Federation of Labor spoke on the usual labor-bureaucrat themes. Then came the president of the Fraternal Order of Police, who said that both police and the labor movement have changed in the past 100 years, and that "the police are now a part of the labor movement." He performed his speech remarkably well amidst the boos, hisses and obscenities hailed at him by anarchists in the crowd.<p>
Is Haymarket Square now one more site to list in guidebooks for Chicago tourists? The struggle itself is the greatest of all monuments. Help make Haymarket Square into site of new struggles.<br>
<b>Joseph Grim Feinberg, </b>Chicago<p>

