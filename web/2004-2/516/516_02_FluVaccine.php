<font face="Times New Roman, Times, serif" size="5"><b>Behind the flu vaccine scandal</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Leighton Christiansen</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 15, 2004 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE OCTOBER 5 announcement that half of the U.S. supply of flu vaccine is possibly contaminated and therefore unavailable made headlines in the U.S. media. But nobody was telling the real story--that the flu vaccine scandal is another facet of an already existing disaster, known as the for-profit U.S. health care system.<p>
Britain's Medicines and Healthcare Products Regulatory Agency (MHPRA) suspended California-based Chiron Corp.'s license to produce influenza vaccine at a plant in Liverpool, England, after finding evidence of bacterial contamination. Incredibly, Chiron is one of only two companies supplying flu vaccine to the U.S. It was expected to supply some 48 million doses to the U.S. this flu season--just about half of the total order for the vaccine.<p>
The British MHPRA found contaminated vaccine batches back in August. But Chiron CEO Howard Pien, with a worried eye on company profits, claimed as recently as late September that the contamination problem had been cleaned up.<p>
The Liverpool factory that was shut down is more than 30 years old and has a history of contamination problems under a number of previous owners before Chiron purchased it in 2003. In 2000, polio vaccines manufactured at the factory were recalled because they were contaminated with mad cow disease.<p>
So U.S. hospitals and clinics can simply order more vaccine from other drug makers, right? Wrong. There are only a dozen or influenza vaccine manufacturing plants in the world. They use 30-year-old technology and millions of poultry eggs to grow the virus, a process that takes months.<p>
According to the World Health Organization (WHO), total annual global production is 260 million vaccine doses. That is only enough vaccine for 4 percent of the world's 6.2 billion people. And you can bet that the vast bulk of that vaccine goes to wealthy countries.<p>
Eighty-seven million Americans received vaccinations in 2003. Of those millions not vaccinated, 100,000 to 200,000 were hospitalized after falling ill, and 36,000 died. Around the globe, influenza and its complications kill 250,000 to 500,000 annually.<p>
Meanwhile a grave new flu threat exists--a virulent new strain decimating the Asian domestic poultry industry, which could, scientists fear, mutate into a form that can be passed on by humans. Left-wing author Mike Davis warns that this strain could start a catastrophic pandemic, with death rates "comparable to Ebola fever and other nightmare emergent diseases." Yet one basic defense against this--making sure that Asian countries at the epicenter of the threat have adequate supplies of vaccine for less virulent strains of the flu, which could slow the spread of more virulent strains--is being ignored.<p>
Why is the vaccine supply so fragile? Profits--or in this case, a lack of profits. Drug companies have avoided putting money into flu vaccine research and development--or have gotten out of the vaccine business altogether--because flu outbreaks are too unpredictable for the drug makers to make money off vaccines.<p>
Are public health officials clamoring for the government to take over of vaccine production? Quite the opposite. "We have to realize that the era of dirt cheap vaccines is over," said Dr. William Schaffner, a member of the National Vaccine Advisory Committee. "We have to be willing to pay more for the wonderful protection we get from vaccines. When there is more profit, it will be an incentive for companies to enter the market."<p>
Even the World Health Organization accepts this free-market econo-babble. Its recommendation for lower vaccine costs, given at a March 2004 emergency meeting, is "[t]he outsourcing of certain production steps, such as filling and packaging."<p>
George Bush and his pale reflection John Kerry continues to cry wolf about terrorists using biological weapons of mass destruction--while a proven biological threat is all but ignored. It is far past time to take the private profits out of public health care.<p>

