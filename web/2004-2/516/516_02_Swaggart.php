<font face="Times New Roman, Times, serif" size="5"><b>A religious fanatic spewing hatred</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 15, 2004 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">A RELIGIOUS fanatic makes a terrorist threat against a large number of Americans.<p>
Of course, we're <i>not </i>talking about antiwar Muslim Yusef Islam--the singer formerly known as Cat Stevens--who was deported after the Feds said he turned up on their "no-fly" list. We mean televangelist Jimmy Swaggart, the fundamentalist hypocrite who was caught "in sin" in the 1980s when he was busted with a prostitute.<p>
Now, in a recent televised sermon, Swaggart condemned homosexuality as sinful and disgusting--even more so than murder, apparently. If a gay man ever looked at him "like that," he told his flock, "I'm gonna kill him and tell God he died."<p>
Swaggart later "apologized"--if you could call it that. "If it's an insult, I certainly didn't think it was, but if they are offended, then I certainly offer an apology," he blathered.<p>
While the media honed in on Yusef Islam's support for Palestinian causes as proof that he must be an "extremist," they almost universally ignored Swaggart's bigoted outburst. The message is clear: moderate, antiwar Muslims like Islam can be labeled "terrorists" at the drop of a hat, while Christian fanatics like Swaggart can get away with advocating gay-bashing.<p>
It's not hard to figure out who the real terrorist is.<p>

