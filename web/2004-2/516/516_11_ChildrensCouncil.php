<font face="Times New Roman, Times, serif" size="5"><b>Organizing a victory from the ground up</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Maiysha Dickerson and Matt Swagler, CALPRO Local 2345</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 15, 2004 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--The 75 rank-and-file employees of the Children's Council of San Francisco (CCSF) ratified a new contract with a 4 percent pay increase, despite looming state and city budget cuts that forced city workers and most social services employees to accept concessions and cutbacks this year.<p>
The staff of CCSF, a non-profit social services agency, are members of CALPRO Local 2345, a statewide section of the International Union of Painters and Allied Trades (IUPAT). But because the local has only one paid representative who's not even based anywhere near the Bay Area, CCSF staff had to take it upon themselves to organize job actions to win their demands.<p>
In light of pending state budget cuts, management came to the negotiating table proposing a 1 percent increase for current employees and a layoff. Although there's not much tradition of organized rank-and-file action, CCSF employees--faced with continuing increases in the cost of living and a growing divide between staff and management salaries--decided they would rather risk a long fight with management than settle for the offers being presented at the negotiating table. <p>
Employees formed their own rank-and-file negotiating team and, as the fight heated up, formed a rank-and-file "organizing team" to coordinate staff job actions. When managers suggested that the staff already lived in relative comfort and shouldn't need a raise, we produced personal budgets and testimonials from family members to document the struggle of trying to survive on our wages in the Bay Area. <p>
When it became obvious that management was close to their final offer but nowhere near meeting our demands, the organizing team talked to each employee one-on-one, during breaks and over lunch, convincing almost 90 percent of the staff to demonstrate in front of the building the next week, while securing outside community leaders and local union members as speakers. <p>
When management saw employees making picket signs in the lunchroom, they quickly agreed to return to the negotiating table. Wearing buttons with "Equal pay for equal work" and "Social justice: It's in my job description, but not in my paycheck," 40 staff members picketed in the crowded hallway outside the negotiations. <p>
Under this pressure, management caved in to staff demands for a cost-of-living increase and ultimately agreed to a 4 percent increase in pay. <p>
Next year will likely bring the threat of more funding cuts to the agency, and the entire contract will be open for negotiation, not simply the yearly negotiation over wages and benefits. "We need to get ready for next year right now and keep the momentum going," said organizing team member Maria Lopez. "We frightened them this year, but it will take even more pressure to win the kind of contract that we really need."<p>

