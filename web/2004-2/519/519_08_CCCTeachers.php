<font face="Times New Roman, Times, serif" size="4">Overworked and underpaid City College teachers</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>They deserve respect</b></font><p>
<p><font face="Arial, Helvetica, sans-serif" size="2">November 5, 2004 | Page 8</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker</i>,</b> <br>
It's pretty ridiculous when college professors are forced to strike because they receive no respect from administrators. <p>
I was a student at Wilbur Wright College, one of the City Colleges of Chicago (CCC), from 1999-2001. I am currently a tutor at the same location. When I was a student, I saw firsthand how overcrowded classes were. One class was so overcrowded that there were students standing in the doorway. It was literally a fire hazard. <p>
Chancellor Wayne Watson claims that CCC full-time professors want to continue teaching 12 credit hours a week while other state college professors teach 15 credit hours (which is what he wants full-time professors to do). Newsflash to the administration: Chicago is a huge city--one of the largest in the world. More than 60,000 students enter through the CCC system each year. There are students who even travel from far suburbs to take classes here! <p>
Community colleges in other Illinois cities like Peoria, Rockford and Waukegan don't have to worry about overcrowding; their class sizes are relatively small. Thus, they can afford to teach 15 credit hours a week. CCC professors must teach up to and sometimes more than 42 students in each class, not to mention the fact that they must prepare curriculums, grade assignments, record grades and set time aside for office hours. <p>
They're so overworked from serving so many students that they cannot give them all the help they need. Regardless, they are committed to giving students all they have. I have seen their devotion. <p>
How does Chancellor Watson thank them? By making their yoke even heavier. Doesn't the Thirteenth Amendment outlaw slavery?<p>
The teachers don't want to be on strike, but they need to stand up for themselves and the students. They are on the picket lines for them, so that they receive quality education. A society governed by ignorance is a society doomed to failure. Show the teachers some respect.<br>
<b>Devin Love-Andrews, </b>Tutor, Wilbur Wright College, Chicago<p>

