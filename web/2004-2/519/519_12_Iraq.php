<font face="Times New Roman, Times, serif" size="4">100,000 dead in Washington's slaughter in Iraq</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Killed for oil and empire</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 5, 2004 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">ONE HUNDRED thousand Iraqis are dead. And untold thousands more will be killed in the coming months.<p>
That's the stunning estimate of the civilian death toll in Washington's "liberation" of Iraq--a far higher number than any mainstream source has yet produced. According to a survey of Iraqi households by the Johns Hopkins Bloomberg School of Public Health published in Britain's <i>Lancet</i> medical journal, there were an estimated 100,000 "excess deaths" in Iraq in the 18 months since the invasion began, mainly caused by U.S. air strikes on towns and cities.<p>
The number shocked even researchers. "The use of air power in areas with lots of civilians appears to be killing a lot of women and children," Les Roberts, of Johns Hopkins, bluntly told Reuters. Overall, researchers found that the risk of death from violence in the period after the invasion was 58 times higher than before the war. <p>
As Iraq expert Juan Cole pointed out, "The troubling thing about these results is that they suggest that the U.S. may soon catch up with Saddam Hussein in the number of civilians killed. How many deaths to blame on Saddam is controversial...But if we exclude deaths of soldiers, it is often alleged that Saddam killed 300,000 civilians...But if [Johns Hopkins researchers] are right, the U.S. has already killed a third as many Iraqi civilians in 18 months as Saddam killed in 24 years."<p>
According to the study, two-thirds of violent deaths were in Falluja, a stronghold of opposition since the early days of the invasion.<br>
The U.S. has been hammering Falluja with a two-month-long campaign of air assaults that nightly pulverize the city's defenseless population with 500-pound laser- and satellite-guided bombs.<p>
And if the U.S. and their puppets in Iraq have their way, there will be many more dead Iraqis in the coming weeks. The U.S. is reportedly preparing an all-out offensive to crush the resistance in Falluja and nearby Ramadi before Iraq's scheduled January elections. If the attack takes place, it will mean a civilian slaughter--in a city that has already been under military siege for weeks. <p>
"We are merely trying to live," truck driver Hamid Taleb Shal al-Zubayi told the <i>Boston Globe</i>. "What sin have we committed to deserve this?" Al-Zubayi was driving his pregnant daughter to a midwife in Falluja in early October when the family was targeted by snipers. A bullet struck his wife in the hip, and he was forced to leave his daughter at a relative's house while rushing to get help for his wife.<p>
Yet a new assault on Falluja, according to the <i>New York Times</i>, would entail "thousands of Marines and soldiers, joined by thousands of newly trained and equipped Iraqi soldiers, police officers and commandos, [attacking] Falluja from multiple directions [and] unleashing direct tank, artillery and mortar fire against insurgent positions."<p>
Navy FA-18s and Air Force F-16s and F-15Es would carry out air strikes against "insurgent" safe houses, weapons caches and other "leadership targets," according to press reports. But to judge from the carnage in Falluja to this point, the U.S. military is bombing anything that moves--and innocent residents are suffering the brunt of the onslaught.<p>
"Hospitals have all but run out of supplies, and most people know this," one reporter inside the city told BBC News in mid-October. "Hospital workers clean the floor after receiving fresh casualties, but still the injured are being taken there--just so that they can be near the doctors and receive some comfort. The Iraqi health ministry has not sent any extra supplies. Food supplies are also running out. All shops are shut. Some people who fled the city a few days ago have begun returning because they ran out of food. They are coming back even as more and more people are trying to leave."<p>
As Brig. Gen. Sabah Naji al-Janabi, Falluja's chief of police, told the <i>Boston Globe</i>, "If a ground offensive on Falluja took place, then thousands of people will be killed. If the U.S. keeps insisting on the principle of the stick, there will be an inevitable war in Falluja with regrettable consequences."<p>
Iraqis are paying the price in Washington's war for oil and empire in huge numbers--and if the politicians get their way, even more will die. We have to say no.<p>

