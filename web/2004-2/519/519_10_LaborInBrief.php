<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">November 5, 2004 | Page 10</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Fremont, Calif., teachers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By John Gallagher</b></font><p>

<font face="Times New Roman, Times, serif" size="3">FREMONT, Calif.--Nearly 1,000 Fremont teachers attended a rally in late October to demand that the district come up with a better offer than a 1 percent raise. <p>
Teachers marched from the union office to the school board meeting as people driving by honked their horns in support. All the way, teachers from the Fremont Unified District Teachers Association (FUDTA) chanted: "We're united, strong, and tough. One percent is not enough!" <p>
Fremont is located in the heart of the Silicon Valley, where single-family homes fetch $350,000 to $400,000, far out of reach of a teacher's salary. Recently, Superintendent Dr. John Riekwald cynically commented that home prices in Fremont were too expensive for him to afford on his $250,000 annual salary--a slap in the face to teachers. Moreover, health care prices will continue to rise 20 to 30 percent over the length of the three-year contract. Fremont teachers have not received a raise since June 2003--and they're fed up. <p>
Although union officials and speakers at the rally did not mention the possibility of a strike, teachers who crowded outside the school board did. "If we are pushed to the line, we will [strike]!" said one elementary school teacher.<p>

