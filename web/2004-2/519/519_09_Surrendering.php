<font face="Arial, Helvetica, sans-serif" size="2"><b>MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Surrendering is not a strategy</b></font><p>

<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 5, 2004 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">BEFORE THE outbreak of the First World War, the international socialist movement denounced its coming. They argued that no matter who started the war, it would be a clash between colonial powers fighting over who would control the world's markets.<p>
"If a war threatens to break out," declared the Stuttgart antiwar manifesto of 1907, socialists must "exert every effort to prevent its outbreak." In the event of war, socialists should "intervene for its speedy termination and to strive&#8230;to utilize the economic and political crisis created by the war to rouse the masses and thereby hasten the downfall of capitalist class rule."<p>
Yet, when war broke out in 1914, whole sections of the international socialist movement in the predatory powers broke under the pressure of intense patriotism, collapsing into support for "their own" government. The parliamentary deputies of the biggest section of the movement, the German Social Democratic Party, ended up voting for war credits in the name of "defense of the fatherland," even though Germany clearly waged war to expand its empire.<p>
The principled anti-imperialist left&#8212;led by revolutionaries such as Rosa Luxemburg, Leon Trotsky, Anton Pannekoek and Vladimir Lenin&#8212;had to regroup and rebuild based upon a sharp differentiation from those who had capitulated and renounced the central socialist principle of international workers' solidarity.<p>
Especially damaging were the arguments of the revered and influential German socialist leader Karl Kautsky. Kautsky argued that the Socialist International "cannot prevent" the division of socialists "into different national camps." The only thing socialists could do was wait until the war was over to resume their peacetime activity.<p>
"The international," Kautsky wrote, "is not an effective tool in wartime; in essence it is an instrument of peace."<p>
To this declaration of impotence the revolutionary Rosa Luxemburg replied ironically: "The world historic call of the <i>Communist Manifesto</i> has been substantially enriched and, as corrected by Kautsky, now reads: 'Proletarians of all countries, unite in peacetime and cut each other's throats in wartime!'"<p>
The collapse of Social Democracy during the First World War was a tragedy. The collapse of the independent left into the Democratic Party in this election was more farce than tragedy.<p>
Nevertheless it has parallels. Many influential progressives, having once declared themselves independents, stumped for John Kerry and his Democratic Party on the grounds of "exceptional" circumstances.<p>
The essence of the argument was that the only "realistic" way to get rid of Bush was to vote for the "electable" candidate. Or to paraphrase Kautsky: The left in the U.S. is not an effective tool in election time; it must capitulate to a pro-war, pro-business party as the "lesser-evil," and then resume its oppositional stance after the election is over.<p>
To which a modern Rosa Luxemburg might ironically reply that our new slogan might be: Progressives&#8212;fight <i>for</i> your demands in between elections; but come election time, vote <i>against</i> them.<p>
To paraphrase the revolutionary Leon Trotsky, a left that argues it is acceptable to renounce principles in "exceptional" moments like wars, revolutions or even elections, is like a raincoat with holes&#8212;it doesn't work precisely when you need it (i.e., when it's raining), but it's really effective in dry weather.<p>
The abandonment of independent candidate Ralph Nader for Kerry was surrender disguised as strategy, giving Kerry the opportunity to wax warlike every chance he could, while a large chunk of the left made excuses for him. <p>
Instead of using the election to build the mass antiwar sentiment that hit the streets in February 2003, the left capitulated to Kerry. That squandering of a golden opportunity has helped to <i>disorganize</i> our forces.<p>
Now that Bush is back in office, we're going to have a lot of holes to repair if we're going to have a workable raincoat.<br>

