<P><font face="Times New Roman, Times, serif" size="5"><b>Making shelters in NYC "less inviting"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 9, 2004  | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IN LATE June, New York Mayor Michael Bloomberg unveiled a new "plan" to reduce the city's homeless population--by making shelters less appealing. Bloomberg tried to scare up support for the new scheme by pledging to build 12,000 new units of low-income housing in the coming decade. Yet this number will only meet 75 percent of the city's current need.</P>
<P>The real crime is that Bloomberg gave no details about how the city would come up with funding for the new construction--other than by cutting the budget of the city's existing shelter services. "Our own policies needlessly encourage entry and prolong dependence on shelters," said Bloomberg. "That has created a growing burden on our city budget."</P>
<P>So Bloomberg has ordered shelters to begin asking homeless men to "seek other options" if they can. The new plan would also make it harder for homeless families to receive federal housing subsidies.</P>
<P>All this comes as New York's homeless shelters are housing a record 38,000 people even before the seasonal growth in homelessness takes place in the coming months. Of course, Bloomberg offered nothing to address the root cause of homelessness--the growing gap between low wages and skyrocketing rents.</P>


