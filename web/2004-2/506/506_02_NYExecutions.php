<P><font face="Times New Roman, Times, serif" size="4">Death penalty law overturned</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Executions halted in New York state</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Delphine Selles</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 9, 2004  | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK's highest court ruled that the state's death penalty law is against the state's constitution. The New York Court of Appeals nullified the death sentence of Stephen LaValle, a Long Island man who was found guilty of a 1997 rape and murder. </P>
<P>Their ruling argued that the state's death penalty law--passed under Republican Gov. George Pataki in 1995--pushes jurors toward choosing the death penalty by requiring judges, in the event of a jury deadlock in a capital trial, to give a defendant a sentence that includes the possibility of parole.</P>
<P>This is one more proof that New York's death penalty is deeply flawed. Although no one has been executed in New York since the reinstatement of the death penalty and only four people have been sent to death row, the state's execution machine is plainly biased and racist. </P>
<P>For instance, upstate prosecutors are four times more likely to seek the death penalty than downstate prosecutors. And while whites were the victims in only 32 percent of first-degree murder cases from 1995 to 2001, 60 percent of cases in which a prosecutor sought the death penalty involved a white victim. </P>
<P>The court's decision will put a temporary moratorium on this broken execution machine--effectively emptying New York's death row and hating nine pending capital prosecutions, as well as 12 other cases where prosecutors are seeking the death penalty.</P>
<P>No wonder Pataki expressed "disappointment" at the ruling. Like many politicians, he used the death penalty and "tough on crime" rhetoric to promote his career. The court's decision is a blow to Pataki and other politicians who call for blood in New York.</P>
<P>We won an effective moratorium on death penalty--it's time to continue the fight to abolish executions once and for all.</P>


