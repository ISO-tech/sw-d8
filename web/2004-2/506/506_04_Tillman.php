<P><font face="Times New Roman, Times, serif" size="5"><b>Lying about Tillman's death to sell their war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 9, 2004  | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
When Army Ranger Pat Tillman was killed April 22 in Afghanistan, military officials thought they had the perfect script to attract new recruits. There was much front-page hoopla by the pro-war media about the death of the former Arizona Cardinals star who had turned down a $3.5 million contract to join the Army after September 11. </P>
<P>He was awarded a posthumous Silver Star for combat valor. Army officials reported that Tillman was "killed while charging at the enemy up a hill, allowing the rest of his platoon to escape alive." But like the story of the Jessica Lynch rescue, the script was pure fiction. </P>
<P>Over a month later, Army Lt. Gen. Philip Kensinger Jr. admitted that Tillman "probably" died from friendly fire--a particularly nasty euphemism for being shot at by your buddies-- "while his unit was engaged in combat with enemy forces."</P>
<P>According to Rick Telander of the <I>Chicago Sun-Times</I>, two other Rangers were also killed. No mention of posthumous Silver Stars for the two "regular" dead soldiers, who were left out of the first report so it could be said that Tillman saved them.</P>
<P>Military officials were at a loss to explain the difference between the two scripts, but Kensinger insisted that Tillman was still a big-time hero. "Corporal Tillman was shot and killed while responding to enemy fire without regard for his own safety," he said.</P>
<P>This new version appeared deep in the bowels of local papers, and at the very end is a more disturbing script. An Afghan military official told the Associated Press that there "were no enemy forces." He described Tillman's death in a wild exchange of fire between American and Afghan soldiers after the explosion of a land mine--a classic screw-up resulting from one of the U.S.'s favorite weapons for "collateral damage."</P>
<P>The ruling classes have manipulated the concept of heroism throughout the ages to enlist men to fight for the goals of the rich. The real heroes today are those brave men and women on both sides who fight to end the imperialist aims of the U.S. around the world.<BR>
<B>Cindy Beringer, </B>Austin, Texas</P>

