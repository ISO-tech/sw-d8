<p><a name="Cobb"></a><font face="Times New Roman, Times, serif" size="4"><b>Backing Cobb was the right move</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I usually find your articles thought provoking, but a recent article by Alan Maass on the Green convention ("The Green Party's step backward," <I>SW,</I> July 2) seemed so off the mark. I would say that, rather than a step backward, it is a step forward after two steps backward (1996, 2000). How can a cult of personality (Nader) form a lasting challenge to the duopoly?</P>
<P>This year, I am supporting the Greens specifically because they did not take the Nader route. Nader is a reformist in the true sense of the word. He believes, drawing on the 1930s and before, that the role of third parties and independent movements is to push the two parties leftward. </P>
<P>He clearly sees his historical role in moving the Democrats from its current DLC position to more of a "Roosevelt" party. The Greens, on the other hand, are one of the most viable democratic socialist-leaning third parties in our times. If we are truly concerned about developing a left-leaning opposition to the current duopoly, it has to be with a viable third party, not the cult of personality.<BR>
<B>Nate Schmolze,</B> From the Internet</P>

