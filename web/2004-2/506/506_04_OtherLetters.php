<p><a name="Cash"></a><font face="Times New Roman, Times, serif" size="4"><b>Corporate cash for Kerry</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Sen. John Kerry has received several million dollars for his presidential campaign. The largest donor has been a Boston-based legal group called Mintz, Levin, Cohn, Ferris, Glovsky and Popeo, donating $232,736.</P>
<P>There are two companies that this legal group has gotten its fingers in--the Cellular Telecommunications and Internet Association (CTIA) and Citistreet. The CTIA is the "voice" of the wireless industry. They represent a number of companies before the Federal Communications Commission (FCC) and Congress. Citistreet is a branch of the company Citigroup.</P>
<P>Mintz has represented the CTIA in a number of cases before the FCC. And since the CTIA is the "voice" of the wireless industry and their purpose is representing the large number of telecommunications and Internet services to the FCC and Congress, the Mintz legal group is a large part of that voice.</P>
<P>It's no coincidence that John Kerry has sponsored two bills and cosponsored six others in the Senate that are in favor of the CTIA. Also, it's no coincidence that Cameron Kerry (John's brother) is a member of the Mintz legal group. One of Cameron Kerry's duties is as a litigator representing telecommunication industries before the FCC.</P>
<P>Another major company that is dangling Kerry by the strings is Citigroup. Citistreet is a client of the Mintz legal group. It's no coincidence that Citigroup, the parent company of Citistreet, is the ninth largest donor to Kerry, contributing $116,656.</P>
<P>FleetBoston Financial Corporation, a major bank in Boston, is the second largest contributor to Kerry's campaign donating $182,937. Another interesting fact is that FleetBoston is a client of the advertising agency Hill, Holliday, Connors, Cosmopulos, Inc. This advertising agency is number seven on the donation list for Kerry, contributing $122,300. </P>
<P>Time Warner is one of the leading names in entertainment and is also the third largest contributor to Kerry's campaign, donating $145,435. Richard Parsons is the chairman of the board and CEO of Time Warner. He is on a number of other boards, including--you guessed it--Citibank.</P>
<P>Think of this web as you like, but I like to think that these connections are all part of the corporate control that has befallen Washington. Politics are run by special interests, because once they contribute they want something back. I won't be surprised to see Citigroup and CTIA grow significantly if Kerry wins the election.<BR>
<B>Frank Steves, </B>From the Internet</P>

