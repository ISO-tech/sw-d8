<p><a name="Out"></a><font face="Times New Roman, Times, serif" size="4"><b>The U.S. needs to get out of Iraq</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I never wanted anyone to invade Iraq. Once there, I never wanted the "coalition of the willing" to stay. I wanted an international peacekeeping force with NATO and United Nations assistance.</P>
<P>Now I just want the U.S. and every other foreign nation out of Iraq. Let the Iraqis sort out their own future. If it doesn't include democracy, too damn bad. If it doesn't include cheap oil for the U.S., too damn bad. </P>
<P>If another country was doing to America what we are doing to Iraq, I suppose I would be one of the--what are they called now?--"terrorists" fighting against the will of the United States. Get out! Get out now! </P>
<P>I was alive when America dropped the bomb on Hiroshima and Nagasaki and have not accepted that fact yet, so you can imagine I'm having trouble with Iraq and the whole Middle East thing. Change is good. Change is natural. Change is blowing in the wind. Peace.<BR>
<B>Jeff Morgan,</B> Rochester, Minn. </P>

