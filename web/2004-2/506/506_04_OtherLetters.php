<P><font face="Times New Roman, Times, serif" size="4"><b>Cheerleaders for empire</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
<I>Foreign Policy </I>magazine is at it again. Months after publishing Samuel P. Huntington's anti-immigrant screed "The Hispanic Challenge," the latest issue features Niall Ferguson's argument that U.S. Empire is the only thing standing between us and a new Dark Ages.</P>
<P>Ferguson--whose nostalgia for British imperialism led him to write his book <I>Empire</I>--claims that the U.S.'s most likely challengers, China and the European Union, are too weak economically to take up the role of superpower. With no major force dominating the world, he claims that the world will be wracked by terrorism, and that poorer countries will fall under the sway of various kinds of religious fundamentalism.</P>
<P>This is nothing but a lot of scare-mongering. The struggle in the world's poorest places are driven by opposition to neoliberalism and free trade--which is what really keeps ruling-class lapdogs like Ferguson awake at night. And the terrorists in the government who murdered tens of thousands in Iraq and Afghanistan get by without a breath of condemnation from empire's apologists.</P>
<P>There's one thing I hope Ferguson is right about--that the U.S. empire is teetering on the brink. Let's do what we can to push it over the edge.<BR>
<B>Andrew Jagunich,</B> New York City</P>

