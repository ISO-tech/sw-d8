<p><a name="Resist"></a><font face="Times New Roman, Times, serif" size="4"><b>Supporting the right to resist</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I think Eric Ruder left out a crucial point in his answer to the question "Should we support Iraqis when they resist the occupation using armed force?" ("The right to resist," <I>SW</I>, July 2). Ruder makes excellent points about the nature of the resistance, where it has come from and the role that armed resistance has played in history. </P>
<P>But I think it's also crucial to point out that you need not support the resistance--meaning, support the tactics it uses or the ideologies it employs--in order to support the <I>right</I> of the Iraqi people (or any people under occupation) to resist. </P>
<P>Many in the anti-occupation movement, including people with loved ones in the U.S. military, those who believe in pacifism and similar ideologies and those who have other concerns with the nature of the resistance (for example, its affects on Iraqi civilians, etc.), are uncomfortable "supporting the resistance." </P>
<P>Yet all of us in the antiwar and anti-occupation movement can and should recognize and support the fundamental right of an occupied people to resist their occupiers, without having to make a statement either way on the tactics of the resistance. </P>
<P>Furthermore, we can refuse to equate the violence of the oppressed with the violence of the oppressor, and we can refuse to allow differences of opinion about the tactics of the resistance to interfere with our fundamental demand for an end to the occupation of Iraq and real self-determination for the Iraqi people.<BR>
<B>Emily Goldstein,</B> New York City</P>

