<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bipartisan "regime change" in Cuba</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 9, 2004  | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">JUST AS American politicians can never seem to be pro-Israel enough, they can't seem to be anti-Castro enough. And so some lined up on July 1 to praise the Bush administration's decision to impose draconian new regulations limiting contact with the island and curtailing the amount of money Cuban Americans can send to their families.</P>
<P>Republican hack Rep. Lincoln Diaz-Balart called the administration's move "unprecedented--beyond overwhelming." But a sizable number of Cuban Americans oppose the new regulations because they know the only people who will suffer are ordinary Cubans.</P>
<P>Because of the rift in the Cuban population, Bush's hard-line plan--which went into effect only five months before an election in which Florida will prove key--may not reap the number of votes it's designed to.</P>
<P>And so, John Kerry inched a little to the left of Bush in criticizing the plan as "cynical, election-year policy" that would do "nothing to hasten the end of the Castro regime." But as Kerry's statement attests, "regime change" in Cuba has been a bipartisan goal of every U.S. administration since John F. Kennedy's.</P>
<P>During the Cold War, the embargo on Cuba served a bipartisan anticommunist foreign policy. Once the Soviet Union was gone, Cuba lost importance for the U.S. ruling class. </P>
<P>Until Bush's election-year posturing, U.S. business had pursued a gradual policy of opening trade and business links to Cuba--which the "communist" regime, desperate for foreign investment, pursued. "As far as the U.S. ruling class is concerned, they would rush to invest in the island before the Europeans and Canadians had left nothing to invest in," Cuban-born socialist Sam Farber said in an interview in the July/August <I>International Socialist Review.</P>
</I><P>That's why, barring a complete collapse and a threat of massive immigration to the U.S., a Haiti-style U.S. invasion isn't in the cards. More likely is the U.S.'s attempt to create the conditions for a "transition to democracy" in the event of Castro's death.</P>
<P>Fidel has designated his brother Ra&uacute;l, a supporter of China-style "market Stalinism," to take over. Washington would like to prevent that and use the opportunity created by Fidel's death to annex Cuba to a section of the Cuban business elite currently resident in Miami.</P>
<P>Can this happen? And can it happen peacefully? No one knows for sure, but one shouldn't rule out the possibility. Despite using Marxist rhetoric, Cuba is a state capitalist regime in which the government operates as if it were a private firm. And increasingly since the collapse of the Eastern Bloc, elements of private capitalism have emerged around the island.</P>
<P>Key to this development is the role of the army, as Farber points out. The Cuban state recently appointed an army officer to head the country's tourist industry--one of its key earners of foreign exchange.</P>
<P>And Spanish, Canadian--even Israeli--capital has invested in joint ventures with executives from the Cuban state. "I think that Fidel Castro, in order to survive, has created a Pandora's box, since the Cuban executives supposedly working for the Cuban state are meanwhile creating links with big international capital," Farber said.</P>
<P>So this means that in the event of Castro's demise, a whole layer of bureaucrats and generals--all members of the Communist Party--will suddenly emerge as heads of newly privatized firms and heads of "democratic" political parties.</P>
<P>U.S. aid laundered through National Endowment for Democracy-sponsored "civil society" outfits headed by the Democratic and Republican Parties are no doubt already trying the lay the groundwork for this. The "democracy" the U.S. seeks means about as much as "sovereignty" in Iraq these days. And as with the sanctions Bush just imposed, the needs and wishes of ordinary Cubans don't count.</P>
<P>That's why anti-imperialists in the U.S. have to oppose any kind of U.S. manipulation or plans for "regime change" in Cuba. Only the Cuban people--along with Iraqis--have the right to decide what kind of government they want.</P>


