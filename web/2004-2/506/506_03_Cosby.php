<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Cosby's tirades blame the victim</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 9, 2004  | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">BILL COSBY'S diatribes blaming Blacks for the social crisis gripping their communities wouldn't have sounded out of place coming from a racist talk radio "shock jock." "With names like Shaniqua, Taliqua and Mohammed and all of that crap, and all of them are in jail," Cosby snarled the first time he tried out his "new material"--in a May 17 speech at the NAACP's commemoration of the historic 1954 <I>Brown v. Board of Education </I>Supreme Court decision that outlawed segregation in public schools.</P>
<P>"They are standing on the corner, and they can't speak English," he said. "I can't even talk the way these people talk, 'Why you ain't,' 'Where you is'...And I blamed the kid until I heard the mother talk. And then I heard the father talk...Everybody knows it's important to speak English except these knuckleheads. You can't be a doctor with that kind of crap coming out of your mouth."</P>
<P>Cosby doesn't think all of Black America is to blame. "The lower economic people are not holding up their end in this deal," said Cosby. "These people are not parenting."</P>
<P>It came as no surprise that Rush Limbaugh and other conservatives seized on Cosby's tirade as confirmation that Blacks themselves--rather than a racist system--should be blamed for poverty and suffering in African American communities. But it did come as a surprise that no prominent Black leaders condemned Cosby's speech.</P>
<P>In fact, his second tirade took place at Jesse Jackson's Rainbow/PUSH headquarters--with Jackson offering his own defense. "Bill is saying let's fight the right fight, let's level the playing field," Jackson said. "Drunk people can't do that. Illiterate people can't do that."</P>
<P>How can it be that circumstances once understood to be the consequence of racism are today considered--almost universally across the political spectrum--a problem of Blacks not taking "personal responsibility"? One answer has to do with the rise of the tiny segment of Black America that Cosby represents--a very wealthy elite that came to prominence in the aftermath of the Black struggle of the 1960s and '70s.</P>
<P>Cosby has a net worth of more than $500 million, so it's easy to see how he could think that America is color blind--after all, he made it--and the problem is that most Blacks don't try hard enough. In the end, that's what <I>The Cosby Show, </I>his popular 1980s sitcom, was all about--Black professionals living an affluent lifestyle and having fun just like "everybody else."</P>
<P>Cosby's complaints about poor African Americans aren't new. They echo the criticisms historically of wealthy and middle-class Blacks who believe that the real project is to get African Americans to pick themselves up by their bootstraps.</P>
<P>It's a view that comes to dominate in the absence of a social movement demanding justice, equality and fundamental change of America's racist institutions. That more voices haven't condemned Cosby's ravings shows the vast gulf that has developed between the self-appointed spokespeople for Black America and the vast majority of poor and working-class Blacks in the U.S.--and the urgent need to rebuild the struggle for civil rights from the ground up.</P>


