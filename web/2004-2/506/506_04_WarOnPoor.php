<P><font face="Times New Roman, Times, serif" size="4">The everyday brutality of New York City police</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"It's a war on the poor"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 9, 2004  | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Summer is here, and New York City cops are hungry for blood. Recently, as we were getting ready to sell <I>Socialist Worker</I> in Washington Heights, a mostly Latino neighborhood in Harlem, a gang of undercover cops threw a young Black man to the ground between two parked cars, beat him until he was bloody, and then began to laugh and make fun of him. </P>
<P>Two of us ran across the street, asked the gathering crowd what had happened and began to chant "Let him go. Racist cops have got to go!" The man being arrested thanked us for speaking up. The cops yelled "Shut up" and "Mind your own business" at us, as they put their victim into an unmarked van and drove away.</P>
<P>An onlooker named Andre told us that the confrontation started when one of the cops came up to the young man and punched him. The man calmly asked the cops to take it easy and relax, but the cops hit him again, threw him on the ground and started beating him. Andre told us that the cops regularly set up watch on the roof, wait until they see someone buying drugs on the corner, and then come down and arrest the buyer. </P>
<P>About 45 minutes later, the cops put another young Black man in a van. When I started chanting again, one of the cops raised a fist with his middle finger stuck up at me and yelled, "Fuck you! I see you. You're next!"</P>
<P>The cops then circled the van around, pulled up beside us and yelled, "Do you see what's going on here?" and held up a miniscule bag of marijuana. We yelled, "Yeah, it's a war on the poor."</P>
<P>These incidents are just a couple examples of the racist violence that goes on every day in poor and working-class minority neighborhoods in New York City. But we can't look to politicians to do anything about police brutality, because both Democrats and Republicans need the cops to keep poor and working-class people down. </P>
<P>Right now, many people feel too scared to speak out. That is why we have to build a stronger antiwar movement as well that raises the slogan "No to racist war and torture at home and abroad!"<BR>
<B>Sarah Hines, </B>New York City</P>

