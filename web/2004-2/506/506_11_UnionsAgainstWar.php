<P><font face="Times New Roman, Times, serif" size="5"><b>SEIU and AFSCME oppose war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 9, 2004  | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE CONVENTIONS of the two biggest unions in the AFL-CIO last month voted to call for an end to the U.S. occupation of Iraq and to bring the troops home. The resolutions highlight the changes in organized labor since the Vietnam War era, when almost every major union remained behind the war. Meeting in San Francisco, the 1.6 million-member Service Employees International Union (SEIU) unanimously passed a resolution June 22 backing the principles of U.S. Labor Against the War (USLAW).</P>
<P>"Our nation faces growing domestic challenges--unemployment, declining wages and benefits, deunionization of the workforce, reduced public services, crumbling health care and educational systems, cuts in veterans benefits, escalating public debt, and decreased economic, social and personal security," the resolution stated.</P>
<P>"We cannot solve these economic and social problems without addressing U.S. foreign policy and its consequences." The resolution was backed by several key locals, including Local 250, based in Northern California, and New York's Hospital and Health Care Local 1199, which opposed the Vietnam War.</P>
<P>The same week as the SEIU action, delegates to the 1.4 million-member American Federation of State, County and Municipal Employees (AFSCME) voted for an antiwar resolution of their own. The AFSCME resolution stated that Bush "lied to all of us--to the world--about the weapons of mass destruction and why we're going to war in Iraq." </P>
<P>According to an account by Steve Edwards, president of AFSCME Local 2858 in Chicago, "Three locals submitted resolutions, one attacking the principle and practice of pre-emptive war as 'conquest and neo-colonizing an oil rich Arab nation'...while the other resolutions called for U.S. troops to be withdrawn from Iraq 'now.'" The International leadership combined the resolutions, but added wording calling on George W. Bush to "bring our troops home as soon as possible."</P>
<P>Edwards, a member of Chicago Labor Against the War (CLAW), and Earl Silbar of Local 3506, another CLAW member, worked with several delegates to change the resolution to call for a withdrawal of the troops "now." Brenda Stokely, president of AFSCME District Council 1707 and co-convener of New York City Labor Against the War, spoke in favor of the change, which passed overwhelmingly.</P>
<P>AFSCME President Gerald McEntee declared that "the war in Iraq is one of many, many reasons we need a new president." But Democratic presidential candidate John Kerry--who addressed both the SEIU and AFSCME conventions--has virtually the same policy on Iraq as Bush.</P>
<P>The antiwar activists who pushed the resolutions through the conventions had to overcome considerable resistance from top union officials who would prefer to keep quiet on the war so as not to embarrass Kerry. But with the two biggest unions now on record opposing the occupation, the door is open for more antiwar organizing in the labor movement. </P>


