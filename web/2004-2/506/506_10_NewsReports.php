<P><font face="Times New Roman, Times, serif" size="4"><b>End the occupation of Iraq</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Laura Woodward</b></font> </P>

<P><font face="Times New Roman, Times, serif" size="3">SAN DIEGO--"Bring the troops home!" was the rallying cry of more than 150 protesters in the downtown area on June 26. Several groups organized the rally, including Military Families Speak Out, Veterans for Peace, the San Diego Coalition for Peace and Justice and others. "Now is the time to bring the troops home," said Sonja Rodriguez. "They are dying because of the lies of the Bush administration."</P>
<P>The participation of veterans of the current and former wars was especially significant in this heavily military town. "We live in a divided country, and it is easy for people to disagree with political groups, but you can't disagree with the family members of people who are in Iraq dying," said David Fontaine of Veterans for Peace. "Military families have the legitimacy to demand the attention of their neighbors."</P>
<P>Throughout the day, many protesters argued that the U.S. government must live up to its responsibility to care for soldiers returning from conflicts abroad--a demand tinged with anger at the record of callous disregard the U.S. has shown towards the health care and mental health needs of veterans of other wars.</P>
<P>"There are people making billions of dollars on this war," said Nasser Barghouti of the Arab American Anti-Discrimination Committee, "at the expense of [U.S. troops] and the Iraqi nation." During the march, the crowd enthusiastically took up chants of "No blood for oil" and "Bring the troops home." And many onlookers honked and flashed peace signs in support of the marchers.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>We demand homes for the homeless</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Tara Farley and James York</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SPRINGFIELD, Mass.--About 50 people gathered at city hall June 25 in response to a deadline given by a church bishop to disband "Sanctuary City"--a community of tents on a church lawn organized by homeless men and women and community members. More than 50 tents are homes for nearly 80 homeless people, who have been working with activists to demand that the city help them obtain an abandoned building to renovate and use as a shelter. </P>
<P>"People complain about 'unsightly' homeless men and women," said one resident of Sanctuary City. "But what are truly unsightly are the unused spaces and run-down, boarded-up buildings." Last winter, two people froze to death on the streets of Springfield, showing the city's gross disregard for human rights and dignity.    </P>
<P>Massachusetts Gov. Mitt Romney is threatening to take over Springfield's finances--and force further austerity on city residents, including refusing to negotiate with municipal unions that have been working without contracts for months. City workers and the unemployed must stand in solidarity to force the politicians to recognize and respect human rights. "It could happen to anyone," as one protester put it. "We did not choose this lifestyle, we've worked most of our lives, we want our dignity and we want a place to live."</P>
