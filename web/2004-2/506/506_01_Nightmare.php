<P><font face="Times New Roman, Times, serif" size="5"><b>Nightmare in Bush's "new" Iraq</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 9, 2004  | Page 1</font></P>

<P><font face="Times New Roman, Times, serif" size="3">FOURTEEN MONTHS after the U.S. occupation began, conditions in Iraq are hellish. And even the U.S. government admits it. According to a report released in late June by the General Accounting Office (GAO), in several key areas--including electricity, security and the judicial system--Iraq is worse off under the Americans than under Saddam Hussein.</P>
<P>In 13 of Iraq's 18 provinces--where 20 million of the country's 26 million people live--electricity was available for fewer hours each day in May than before the war began more than a year earlier.</P>
<P>Washington tried to boost the image of its occupation with a handoff of "sovereignty" to its handpicked Iraqi puppets--held two days before the June 30 deadline because of escalating violence. But the "handover" hasn't stopped U.S. forces from raining death from the skies at will.</P>
<P>As <I>Socialist Worker</I> went to press, U.S. jets attacked a house in Falluja--killing as many as 15 civilians in the fourth air strike in the area since June 19. These casualties are among the 13,000 Iraqis killed by coalition forces, according to media reports--and that number that doesn't include thousands of soldiers and resistance fighters killed by U.S. troops.</P>
<P>Then there was the beginning of the show trial of Saddam Hussein. This, too, was supposed to prove that Iraqis are back in charge of their own country. Only they aren't.</P>
<P>As <I>Independent </I>journalist Robert Fisk wrote: "Americans hold Saddam Hussein. Americans ran the court in which he appeared. Americans censored the tapes of the hearing. Who do you think is running the country?"</P>
<P>To hear George Bush tell it, the U.S. is not only rebuilding Iraq's infrastructure, but is picking up most of the bill for doing so. Another lie. The U.S. has spent just 2 percent of the $18.4 billion aid package that Congress approved last year after the Bush administration demanded cash to finance Iraq's reconstruction. U.S. occupation authorities did grab money from Iraq--spending or earmarking nearly all of the $20 billion in a special development fund fed by the country's oil sales.</P>
<P>Now the U.S. is counting on its puppet leader, Iyad Allawi, to crack down on any suspected resistance to his U.S. masters by putting new emergency security measures into effect--including curfews, restrictions on movement and "pre-emptive arrests." But no matter how much the U.S. or their Iraqi puppets try to crack down, the resistance to the U.S. occupation will continue.</P>
<P>According to the GAO report, the number of "significant" insurgent attacks skyrocketed from 411 in February to 1,169 in May. On the day that Ambassador John Negroponte took over for occupation overseer Paul Bremer, insurgents killed at least three U.S. Marines. Negroponte immediately declared that one of his biggest goals would be to help defeat "terrorists and criminal elements who oppose a free Iraq."</P>
<P>But behind the scenes, the U.S. is trying to stay in control. "The ideology is gone. The ambitions are gone. We've no aims left," one anonymous U.S. official at the former CPA headquarters (now the U.S. embassy) told Robert Fisk last week. "We're living from one day to the next. All we're trying to do now--our only goal--is to keep the lid on until January 2005 [when the first Iraqi elections are supposed to be held]. That's our only aim--get past the elections--and then get the hell out."</P>
<P>That's cold comfort to the loved ones of the more than 850 U.S. soldiers killed in Iraq--and the thousands of service members maimed in the war. Then there are the war-related illnesses that can't be seen. A study published in the <I>New England Journal of Medicine </I>at the beginning of July found that a staggering 17 percent--nearly one in five--U.S. combat troops returning from Iraq was suffering from post-traumatic stress disorder, major depression or other serious mental afflictions.</P>
<P>Combat-related mental problems are higher among those who have served in Iraq than in any military action since Vietnam. It's not hard to understand why. According to the report, one in four U.S. Marines surveyed reported killing Iraqi civilians. </P>
<P>More than 85 percent of those in Marine or Army combat units said they knew someone who had been injured or killed, and more than half said they had handled corpses or human remains. It's time to end this horror--by bringing the U.S. troops home now.</P>


