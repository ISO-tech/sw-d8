<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">The shape of Election 2004</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Millionaire vs. billionaire</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">July 9, 2004  | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IT MAY be too early to tell who will win the presidential election in November. But we can guess a few things about the winner.</P>
<P>He will be among the very richest people in the U.S., with hundreds of millions of dollars in personal and family wealth. He will have close ties to Corporate America and the Washington establishment--some probably dating back to friendships made while a member of Yale University's Skull and Bones secret society.</P>
<P>He will be determined to call the shots in the "new" Iraq--and won't hesitate to send more U.S. troops. Likewise, he won't flinch from defending the actions of Washington's favorite ally in the Middle East, Israel--no matter how brutal its terror against Palestinians.</P>
<P>You don't need a crystal ball to divine all this--because these things are true of both George W. Bush and John Kerry. With just about every policy statement and speech, Kerry shows that the mainstream parties' two presidential candidates agree on much more than they disagree.</P>
<P>Yet the differences between Bush and Kerry--however small they might be, where they even exist--are all that we ever hear about. For most liberals and even radicals, Election 2004 has been elevated to the "most important election of our lifetime"--with the future of the free world dependent on whether Bush gets back in the White House.</P>
<P>But the truth is that the election in November won't change that much about American politics. On the occupation of Iraq, for example, Kerry's criticisms are about the whens and hows, rather than the what--how U.S. imperialism's plans for Iraq are being managed, not whether the U.S. should be there in the first place.</P>
<P>Millions of people who rightly oppose the Bush administration for sending the U.S. to war against Iraq for oil and empire are being told that they can stop Bush with their ballots--but they have to vote for a Democrat who is as committed to Washington's drive for oil and empire as Bush. The truth is that voting for Kerry means voting for virtually the same policies pursued by the Bush administration, but with a different face presenting them.</P>
<P>If the 2004 election is the "most important of our lifetime," then you would think that the Democrats would be on the offensive against Bush. Guess again. Instead, they're going after independent candidate Ralph Nader.</P>
<P>Last weekend, Democratic National Committee chair Terry McAuliffe used his time on CBS's <I>Face the Nation</I> to highlight rumors and gossip that Nader is taking money from the Republicans. He neglected to mention that the Democrats had organized a concerted effort to keep Nader off the ballot in every state where he has tried to qualify.</P>
<P>As Florida's Republican Party Chair Carole Jean Jordan told a reporter, "Democrats are quick to use the issue of voter disenfranchisement to their benefit, and yet have no problem unleashing their legal sharks on Ralph Nader." You know the Democrats have sunk to new depths when a Republican from <I>Florida </I>can claim to be a defender of democratic rights.</P>
<P>Attacking Nader isn't anything new for the likes of McAuliffe--whose crew blamed the former Green Party candidate for the defeat suffered by their GOP Lite candidate Al Gore in 2000. But he's not the only one. A number of progressives--including some who supported Nader in 2000--are getting in on the action, with streams of invective against a candidate who has dared to take on the issues that Kerry won't, from the war in Iraq to corporate greed to Washington's shredding of civil liberties.</P>
<P>In fact, the Green Party, which ran Nader as its presidential candidate in 1996 and 2000, rejected him this year--and nominated David Cobb, a "safe-states" candidate who promises not to threaten Kerry in so-called battleground states where a good showing might tip the Electoral College vote to Bush. Cobb's running mate Pat LaMarche is so committed to helping Kerry win that she told a Maine newspaper that she might not even vote for herself if there was a chance it could hurt the Democrats.</P>
<P>Immediately afterward, left-wing columnist and former Nader backer Norman Solomon applauded the Greens' decision to turn its back on Nader. "Midway through 2004, while his electoral base shrinks, one of the great American reformers of the 20th century is drifting out to sea," Solomon wrote.</P>
<P>But it's Solomon who's drifting--away from the principle he once recognized of the need for a political alternative independent of the two corporate parties of the status quo. Building that alternative starts by rejecting the logic of Anybody But Bush.</P>


