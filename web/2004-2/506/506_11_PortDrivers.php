<P><font face="Times New Roman, Times, serif" size="4">Pickets tie up ports from South Carolina to California</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Port drivers sting bosses</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Ken Morgan</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 9, 2004  | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">SHOUTS OF "Shut it down" and "Go home" greeted port truck drivers attempting to move cargo at the Columbus Street Terminal at the port of Charleston, S.C., on June 28. Charleston was just one of several ports affected by owner-operator truck drivers, who move container freight into and out of the nation's ports.</P>
<P>The drivers, who own or lease their trucks, are protesting the loss of income as a result of rising costs, especially from spiking fuel prices; unpaid waiting time, which can be several hours a day; and the denial of the right to union membership and representation as a result of being classified as "independent contractors."</P>
<P>Striking truckers in Charleston estimated that only a quarter of the usual truck-borne freight was being moved. A picket line of about 70 drivers was successful in shutting down a rail facility at Charleston's inter-modal yard when union rail workers, including engineers, honored their picket line.</P>
<P>The Charleston action is already affecting retailers. William Porcher, logistics coordinator for Briggs Plumbing, said, "It's killing us. I've got about 70 boxes sitting on the terminal." The port of Savannah, Ga., was described by the <I>Savannah Morning News</I> as "uncharacteristically quiet" during picketing by striking drivers. "Obviously, volume is down," said a spokesperson at the port of Newark, N.J.--the scene of a picket line of 200 striking drivers.</P>
<P>Strike supporter and driver Susanne Hall told reporters, "I've been doing this for five years, and I'm making the same $35 a move that I was making back then, and you can believe I'm paying a lot more in fuel these days."</P>
<P>In Baltimore, drivers estimated that port traffic was down by half since their action began. In Miami, trucking companies and shippers called for a meeting with the drivers June 29. The next day, striking drivers shut down virtually all container operations. Other actions were reported in ports in New Orleans and Los Angeles. Actions in Oakland may be hampered by the fact that three drivers active in the movement have been served with an injunction against further picketing.</P>
<P>The Teamsters union--while denying that the union called for these actions--urged shipping lines and stevedore companies to compensate drivers for increased fuel costs. The Teamsters are involved in an organizing drive among the 50,000 port drivers on all coasts, but are hampered by labor legislation that prohibits organizing among workers designated as "independent contractors" as a violation of anti-trust legislation.</P>
<P>Following a meeting with port drivers and local Teamster leaders on June 14, International Brotherhood of Teamsters President James Hoffa warned carriers, "Without a swift response, drivers are likely to park their trucks until conditions improve." </P>
<P>While these limited job actions have had some effect on shipping at several ports, only by effectively shutting down the majority of ports will port truck drivers see their grievances addressed. The port drivers planned on continuing nationwide job actions through July 4. </P>


