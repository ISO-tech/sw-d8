<P><font face="Times New Roman, Times, serif" size="5"><b>And who is going to save us from them?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Sheri Pegram</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 9, 2004  | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2"><B><I>Saved: The Movie, </B></I>directed by Brian Dannelly, starring Mandy Moore, Macaulay Culkin and Jena Malone.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">AMERICAN EAGLE Christian High School isn't your average high school. Student activities include painting a statue of Jesus and protesting against an abortion rights rally. In every classroom hangs a picture of George W. Bush and the American flag.</P>
<P>Hillary Faye is the most devoted teen at American Eagle. And she has a huge checklist of people she doesn't approve of--Dean, who is gay; Cassandra, the only Jew in school; and her brother Roland, who is in a wheelchair but rejects sympathy.</P>
<P>The film's main character is Mary, who questions whether there is a God after she finds out she's pregnant. Cassandra and Roland's sighting of Mary in front of the Planned Parenthood gives a taste of <I>Saved's </I>hilarious satire aimed at the Christian Right.</P>
<P>"What's the only reason a Christian girl comes downtown to the Planned Parenthood clinic?" asks Cassandra. "To plant a pipe bomb?" replies Roland. "OK, two reasons," says Cassandra.</P>
<I><P>Saved </I>is about hypocrisy and blind moral righteousness. In it, characters do things because they believe Jesus has told them to do it. Pretty scary stuff if you think about who's in the White House.</P>


