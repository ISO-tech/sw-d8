<font face="Times New Roman, Times, serif" size="5"><b>Bush's hit squad</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 10, 2004 | Page 1</font><p>

<font face="Times New Roman, Times, serif" size="3">IT'S THE new Bush gang. And what a sickening bunch of hawks, grifters and yes men they are.<p>
Over the last few weeks, the Bush administration has been busy filling seats left by eight (and counting) departing cabinet secretaries.<p>
So good riddance to the civil liberties-shredding Attorney General John Ashcroft Goodbye Colin Powell, the so-called "moderate" who peddled lies about Iraq's supposed weapons of mass destruction at the United Nations.<p>
So long, Health and Human Services Secretary Tommy Thompson. We'll remember you when we hear about the 5 million people who lost their insurance between 2000 and 2003. "We touched the third rail of politics," Thompson bragged of the Medicare cuts that he helped push through. As usual, workers and the poor got burned.<p>
Don't let the door hit you on the way out, guys. We know you'll have a grand old time making fists full of dollars in the private sector--or furthering your political ambitions.<p>
But there's no relief in sight for our side--because Bush has a new crop of creeps on the way in.<p>
Replacing Ashcroft is Alberto Gonzales. When Bush was governor of Texas, Gonzales was his chief legal counsel--when meant he was in charge of greasing the rails for the Texas execution machine. As White House counsel, Gonzales set the stage for the abuse of Iraqi prisoners in Abu Ghraib with his expert legal advice on how to get around international laws barring the use of torture.<p>
Then there's Bernard Kerik, who will replace Homeland Security chief Tom Ridge. When Kerik was appointed head of New York City's Department of Corrections in the mid-1990s, an official told the department's commissioner, "Congratulations. You've just hired Rambo."<p>
Kerik is the former security chief for the Saudi royal family's hospitals and an undercover narcotics cop in New York City's corrupt and deadly police department. Kerik was sent to Iraq to train new Iraqi law enforcement officers. Since then, tens of the thousands have deserted.<p>
Of course, a few of Bush's most important henchmen--and henchwomen--are here to stay. Hold on, says Donald Rumsfeld, I'm not going anywhere. And while Condoleeza Rice will move to a different office, she'll still be authoring the administration's plans for a war on the world.<p>
These are the goons in charge of imposing the Bush agenda--from destroying Social Security to making their gigantic tax cuts for the rich permanent, from continuing the bloody occupation of Iraq to waging more wars in the name of "fighting terrorism"<p>
The new Bush administration--like the old one--thinks it can get away with murder. It's up to activists to challenge Bush's hit squad every step of the way--and show that they can be stopped.<p>

