<font face="Times New Roman, Times, serif" size="5"><b>Why California's Prop 66 was defeated?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 10, 2004 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
Proposition 66, on the ballot in California this past November 2, would have amended this state's draconian "three strikes" law so that judges could no longer use nonviolent felony conviction to throw people away for 25 years to life. Unfortunately, the referendum failed by a tiny 2 percent margin.<p>
With less than a month to go until election time, though, voters had been favoring the proposition by a 3-to-1 margin. What happened?<p>
A multimillion-dollar smear campaign was kicked off by the "compassionate conservative Governator" Arnold Schwarzenegger and the Fraternal Order of Police. Just days before the election, they hit the airwaves with blatant lies like, "If Prop 66 passes, rapists will be back on the streets." (Remember, Prop 66 was going to retroactively re-sentence <i>nonviolent</i> offenders!)<p>
Prominent Democrats like California Attorney General Bill Lockyer joined the campaign against Prop 66. Most disgustingly, Democrats with liberal credentials went right along with the dishonest, right-wing campaign. Liberal City Council member Antonio Villaraigosa, a mayoral candidate and "labor champion of LA," did a phone blast to Latinos with a message, in Spanish, warning people to vote "No" on Prop 66.<p>
Still, only some people bought the lies. When the voting was broken down by county, it showed that the counties on the coast, where all the big cities are, voted <i>for</i> Prop 66. Rural counties inland, where there is the <i>least </i>crime, were the most susceptible to the scaremongering around Prop 66. Those counties voted no.<p>
"This is a sick state of affairs when the Governator can whip up this much fear in the areas least affected by 'three strikes,'" said Donna Warren of Families to Amend California's Three Strikes. "But we'll have to keep fighting because we came very, very close."<br>
<b>Sarah Knopp, </b>Los Angeles<p>

