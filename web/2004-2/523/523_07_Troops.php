<font face="Times New Roman, Times, serif" size="5"><b>The horrible toll on U.S. troops</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 10, 2004 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">LAST JUNE, Iraq veteran Jeffrey Lucey went into the basement of his family's Massachusetts home and hung himself. The 23-year-old Marine was subsumed with guilt after killing two unarmed Iraqi soldiers last year. <p>
He told his sister he looked the men in the eyes before closing his own eyes and pulling the trigger. He threw the two dead soldiers' dog tags at her and shouted, "Don't you understand? Your brother is a murderer!"<p>
The Pentagon does not track suicides among returning soldiers, but has already coldly calculated that a flood of Iraq veterans who survive the war will be fighting its ghastly memories for the rest of their lives, and many will lose the battle.<p>
The Army News Service reported in March, "Soldiers indicated their most troubling experiences in combat came from seeing dead bodies (67 percent), being shot at (63 percent), being attacked or ambushed (61 percent) and knowing someone who was killed or seriously wounded (59 percent)." Roughly 90 percent of U.S. troops in Iraq have been involved in a firefight, and already 20 percent of Iraq veterans seeking Veterans Administration (VA) care need mental health treatment.<p>
As in Vietnam, the prevalence of post-traumatic stress disorder (PTSD) is a product of the character of the war, in which U.S. soldiers are under orders to terrorize an entire population, fighting a growing and well-organized resistance.<p>
Anticipating a surge in PTSD, the VA announced that it will place a psychiatrist or psychologist on staff full-time at all its 856 outpatient clinics, revitalize substance abuse treatment programs and provide 10,000 spaces for homeless veterans across the country. This will undoubtedly prove grossly inadequate to treat what the VA predicts will be an "epidemic" of PTSD among active-duty combat troops (average age: 19) in coming years.<p>
National Guard troops and reservists--who make up 40 percent of U.S. troops in Iraq--are offered no organized mental health programs. The U.S. government is already turning its back on returning troops.<p>
In the <i>San Francisco Chronicle, </i>military mom Teri Wills Allison recently described a returning soldier who "routinely has flashbacks in which he smells burning flesh...seeing people's heads squashed like frogs in the middle of the road, or dead and dying women and children, burned, bleeding and dismembered." But "[I]nstead of getting treatment for post-traumatic stress disorder, he has just received a 'less than honorable' discharge from the Army. The rest of his unit redeploys to Iraq in February."<p>
PTSD is also associated with family violence. Yet one soldier's wife told the <i>New Yorker </i>she was advised before her husband's return, "'Don't call us unless your husband is waking you up in the middle of the night with a knife at your throat...He'll have flashbacks. It's normal.'"<p>
Cpl. Travis Friedrichsen, who returned from Iraq in September, soon began to shake uncontrollably while sitting in bed, recalling a bomb that exploded just feet away. Friedrichsen, age 21 and married, now has trouble controlling his temper, "exploding into tirades that he says have gone on for an entire weekend," according to the <i>Chicago Tribune.<p>
</i>Those who equate "supporting our troops" with supporting the war should think again. Shortly before he died in Iraq last month, 28-year-old Marine Staff Sgt. Russell Slay wrote a farewell letter to his family. He told his 5-year-old son to "stay away from the military. I mean it."<p>
According to the Army News Service Survey in March, fully 72 percent of the soldiers said their unit morale was low. In September, a Marine infantryman put it more bluntly to the <i>Christian Science Monitor, "</i>We shouldn't be here. There was no reason for invading this country in the first place...I don't enjoy killing women and children. It's not my thing."<p>
Supporting our troops should mean supporting Veterans for Peace, whose statement of purpose reads, "We know the consequences of American foreign policy because once, at a time in our lives, so many of us carried it out. We find it sad that war seems so delightful, so often to those who have no knowledge of it."<p>

