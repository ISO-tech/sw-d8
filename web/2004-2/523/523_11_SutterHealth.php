<font face="Times New Roman, Times, serif" size="5"><b>Hospital workers locked in fight with Sutter Health</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Andy Libson and Lichi D'Amelio</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 10, 2004 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Service Employees International Union (SEIU) Locals 250 and 707 staged a one-day strike December 1 at nine Northern California hospitals operated by Sutter Health. Sutter, a Sacramento chain that operates 27 hospitals in Northern California, retaliated by locking workers out of their jobs for five days and hiring scabs in the meantime.<p>
Union demands include workers having a say on staffing levels and employer-paid retiree health insurance. Hospital workers are looking to get staffing levels that are safer for workers and patients.<p>
Sutter is notorious for putting profits before patients, netting over $465 million last year alone and dishing out excessive salaries and bonuses to hospital executives. <p>
In the Mission District in San Francisco, workers on the picket line at St. Luke's Hospital, who have been working without a contract since June 30, have staged press conferences. On December 3, they marched to City Hall, where they received support from local supervisors.<p>
"They always want more profit," said Raffy Tan, a housekeeper with Local 250. "They want more now, and they will want more in another three years, and then what? "If we let Sutter win this time, we're definitely not going to win the next time around when our contract expires."<p>
 Solidarity--and the potential for more of it--exists, as Local 250 workers have been joined by a number of other locals and unions, including the California Nurses Association. The union vowed to keep up the pickets until December 6, and to ratchet up public awareness and media attention.<p>
Hospital bosses have hired part-time security and scabs to try to keep hospitals running during the lockout in a clear effort to intimidate workers and get SEIU to back down. The SEIU, meanwhile, has publicized the lockout and the demands of the workers in a bid to expose Sutter's greed. <p>
Andi, an organizer with Local 250, noted the tremendous support that the hospital workers were getting from the community. "People are thinking differently these days," Andi said. "They see the connections with the grocery workers' strike, the hotels workers' strike and our strike. They know they could be next."<p>
Sutter has accused the SEIU of wanting a master contract that would unite contract expiration dates with other hospitals and has made this issue the basis for not bargaining with the union. At an Alta Bates Hospital rally, Sal Roselli, the president of Local 250, took on this charge. Stressing that Sutter is centrally administered and controlled, Roselli said it is disingenuous for the hospitals to cry foul when workers look to create their own centralization and unity.<p>
The union hopes this one-day strike and lockout will encourage the bosses to bargain in good faith. <p>
More than likely, this is just the opening round in a fight between hospital bosses and workers. It will take more determined actions to get Sutter to back down and to win a fair contract. <p>

