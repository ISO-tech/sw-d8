<font face="Times New Roman, Times, serif" size="5"><b>"Money for housing, not for Bush's war!"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 10, 2004 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">WITH SIGNS reading "Billions for the War, Still Nothing for the Poor," dozens of homeless families descended on Philadelphia's main military recruiters' office November 30. <p>
The action was part of the Kensington Welfare Rights Union's (KWRU) Homes for the Holidays: Operation Bring the Money Home campaign.<p>
The KWRU is a homeless and poverty rights group started by a group of poor women in response to state welfare cuts enacted by former Gov. Robert Casey in 1991. The group is based in Kensington, a North Philadelphia neighborhood that was once a thriving manufacturing area, but is now one of the poorest parts of Pennsylvania. KWRU uses civil disobedience, builds tent cities and takes over abandoned houses to pressure the city into providing affordable housing for the homeless.<p>
The day before Thanksgiving, homeless families set up a "Bushville Tent City" in North Philadelphia--recalling the Hoovervilles that poor families were forced to live in during the Great Depression of the 1930s. Over the next few days, KWRU members were joined by other community organizations and supporters, like the Philadelphia Disabled in Action, and Bushville doubled in size.<p>
On November 30, KWRU members went to the local office of the Department of Housing and Urban Development (HUD) to demand that HUD Secretary Alphonso Jackson come to Philadelphia to see in person the impact of the Bush administration's budget cuts.<p>
The administration's 2005 budget for Section 8 housing vouchers is $1.6 billion below the amount needed to maintain the current level of assistance. This means that some 250,000 households could lose vouchers--and many could end up on the streets. Meanwhile, point out KWRU activists, buildings go unused all over Philadelphia.<p>
As soon as activists entered the HUD building, the elevators were shut down, and they were told that they could not meet with officials. The protest moved to the military recruitment offices, where protesters set up their Bushville tents to stay the night.<p>
"The Bush administration continues to put billions of dollars towards a needless, brutal war, while families across America suffer without the basic necessities of life," Cheri Honkala of the KWRU and Poor People's Economic Human Rights Campaign said in a statement. "This is not a fight for a bed in a homeless shelter; it is a fight for decent, affordable housing for everyone in this wealthy nation."<p>
More protests like these will need to be organized--to push back the Bush administration's attack on workers and poor.<p>
<i>For more information about the Kensington Welfare Rights Union, visit their Web site at www.kwru.org.</i> <p>

