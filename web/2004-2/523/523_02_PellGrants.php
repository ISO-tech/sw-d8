<font face="Times New Roman, Times, serif" size="4">New rules push down Pell Grants amounts </font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Students cut off by Bush</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 10, 2004 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">HUNDREDS OF thousands of working-class and poor students will find it harder than ever to pay for college next year. That's because of a provision tucked into an appropriations bill that passed Congress in late November. It cuts $300 billion from the federal Pell Grant program--the largest federal grant program for college students.<p>
Under the new rules, the Pell Grant amounts that many families are eligible to receive will be pushed down due to changes in the formula used by the Department of Education to determine family income. About 1 million students will see their grants cut by about $300, and nearly 90,000 students now receiving Pell Grants will be cut off entirely. Additionally, the maximum Pell Grant award will be frozen at $4,050--for the third year in a row. <p>
Since only those students whose families make less than $40,000 a year even qualify for Pell Grants, these cuts effect poor and working-class families. "After all of the conservatives talk about 'family values,' you'd never expect them to pull the rug out from under the feet of America's neediest students, but that's exactly what they did," Campaign for America's Future Co-Director Robert Borosage said in a statement. "At the same time, this same conservative majority is hell-bent on extending tax breaks which--in 2004 alone--will put more than $30 billion into the pockets of America's wealthiest multimillionaires. Where is the morality in that?"<p>
The new changes are horrible news for the 4.5 million students across the country who receive the grants each year. Students like 19-year-old Jo Ann Swett.<p>
Swett, a University of Cincinnati sophomore, works several part-time jobs to make ends meet and gets financial support from her mother, a thrift-store clerk. But now, she told the <i>Cincinnati Enquirer</i>, she's not sure that she can afford to finish college. "Basically," she said, "the government is saying that there are people that don't have the right to education. They are keeping people from being able to improve their lives."<p>

