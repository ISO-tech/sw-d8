<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>What do Marxists say about morals?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 10, 2004 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">WE ARE offered from politicians of both parties the spectacle of endless rhetoric about the need to promote "moral" and "family" values. <p>
Yet these parties promote policies that make life harder for most families--shrinking access to affordable health care; stagnant or falling wages; declining social services and unemployment benefits; deteriorating education; lack of affordable housing, child care facilities and access to abortion. Both parties backed a war on Iraq based on lies; both parties pass budgets that offer big corporate handouts--welfare for the rich--while they banter about the need to "wean" the poor from of its "culture of dependency."<p>
Of course, no politician will admit that their moral talk papers over immoral acts. Indeed, the whole morality debate is meant to deflect discussion away from government and corporations, and onto individuals or scapegoats. The mainstream talk today of "moral values" is good, old-fashioned victim blaming.<p>
We're taught, however, that morality and ethics are universal, that there are basic "codes" of human behavior, good for all times and all situations--be kind to animals, don't steal, don't kill, take responsibility for your actions, etc. Of course, these moral precepts collapse on contact with reality--it's okay to kill a rabid dog, and in the U.S., for states to kill convicted murderers.<p>
In truth, there is no "universal" morality. Morality is historically conditioned by the society from which it arises, and therefore has changed throughout history. 	Just think of the massive changes in attitudes--and laws--concerning sex out of wedlock or divorce.<p>
"All moral theories," wrote Frederick Engels, " have been hitherto the product, in the last analysis, of the economic conditions of society obtaining at the time. And as society has hitherto moved in class antagonisms, morality has always been class morality; it has...justified the domination and the interests of the ruling class." <p>
The moral theories of the ruling class are profoundly hypocritical. "The ruling class," said Leon Trotsky, "forces its ends upon society and habituates it to considering all those means which contradict its ends as immoral. That is the chief function of official morality." <p>
The incomparably lethal violence of the U.S. government is apparently motivated by the highest moral standards--liberation, freedom, democracy. If any of its unsavory acts are brought into the public light--say, torture--they are dismissed as rogue behavior. On the other hand, the violence of the "enemy" is presented not as legitimate resistance to an unwanted military occupation, but as lurid terror aimed at preventing "democracy" in Iraq.<p>
This is the template used in virtually every mainstream newspaper in the U.S. Domestically, a single mother dependent on government aid to feed her children--because she either can't find work at all or can't find work that would pay enough for her to afford child care--is presented as lazy and immoral. But the government that cuts off her aid and forces her into a homeless shelter is merely giving her a chance to become "independent."<p>
A company that raids its employee pension plan or cuts health benefits (that is, ruins workers' lives in order to boost its bottom line) is merely attempting to create the financial prerequisites for "healthy economic growth." But workers who go on strike to protect their health and financial futures are presented as greedy service disrupters.<p>
For Marxists, the profoundly immoral character of capitalism lies in the fact that it produces tremendous wealth on one side and great poverty on the other--that the condition for the comfort and luxury of a few is based on relentless work, with little reward, for the rest. So to the hypocritical moralizing of the ruling class and its domination, we must counterpose the morality of solidarity and equality--a working-class morality, to quote Engels, which "represented its indignation against this domination and the future interests of the oppressed."<p>

