<font face="Times New Roman, Times, serif" size="5"><b>Keep recruiters off our campuses</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 10, 2004 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">THE MILITARY can't force its way into our colleges and universities. In a 2-1 ruling in late November, the U.S. 3rd Circuit Court of Appeals overturned the Solomon Amendment, a 1995 law that lets the government take federal funding from campuses that bar military recruiters.<p>
Across the country, dozens of schools, particularly law schools, have anti-discrimination clauses barring employers who discriminate on the basis of race, gender or sexual orientation--which the military does by banning gays. But the Solomon Amendment prevents schools from enforcing these clauses against the military.<p>
The law remained largely unenforced during the Clinton administration, but beginning in 2001, the Pentagon sent letters to more than 20 schools, threatening funding cuts if they continued to deny access to military recruiters. By October 2002, the U.S. Air Force had threatened to cut hundreds of millions of dollars of funding from colleges across the country--including $300 million from Columbia University alone.<p>
More than 25 law schools and 900 professors joined together to challenge the law in court. The court found that the Solomon Amendment violates colleges' free-speech rights, by forcing schools to express a message that is "incompatible with their educational objectives."<p>
Ironically, the two-judge majority based its decision on an earlier Supreme Court ruling that the Boy Scouts of America could bar homosexuals from becoming scouts or troop leaders. If the Scouts could legally reject gays, said the court, then other institutions could impose the opposite restriction. <p>
While the legal reasoning is convoluted, this ruling is an opportunity for antiwar activists. We should seize it--and organize now to get campuses out of the business of providing cannon fodder for Washington's wars.<p>

