<font face="Times New Roman, Times, serif" size="4">Flight attendants threatening industry-wide strike</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Showdown in airlines?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 10, 2004 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">AIRLINE FLIGHT attendant unions are standing by their threat of an industry-wide strike if carriers use bankruptcy courts to void union contracts.<p>
Airline unions have already made billions of dollars in pay and work-rule concessions in recent years--but the flight attendants have had enough. "Almost everywhere we look, flight attendants are being forced to work longer hours with reduced rest time, and all for ever-decreasing wages," Patricia Friend, president of the Association of Flight Attendants (AFA), said last month. "This must stop. Our entire industry is in turmoil, and the careers of our flight attendants all hang in the balance."<p>
The AFA--which recently affiliated with the Communications Workers of America (CWA)--mailed out strike ballots December 2 to workers at US Airways and United Airlines.<p>
The union has gotten promises of solidarity actions from flight attendants in other unions--the Association of Professional Flight Attendants at (APFA) at American Airlines and Transport Workers Union (TWU) Local 556 at Southwest Airlines. "We will not stand on the sidelines while airline executives misuse bankruptcy courts to cover up their own mistakes and turn back the clock on the progress that we have all achieved," TWU Local 556 President Thom McDaniel said in a press statement.<p>
The AFA's move was prompted when US Airways and United told unions that they would have to accept the latest in a series of concessions--or see the cuts imposed anyway by a bankruptcy judge, as happened with mechanics at United in 2003. <p>
Unions have already made $1.9 billion in concessions at US Airways over the past two years--and the airline dumped the pilots' pension fund into the government-run Pension Benefit Guaranty Corp. (PBGC), which will pay only a fraction of what the pilots are owed.<p>
United is taking the same approach. It's already extracted $5 billion in concessions and 20,000 job cuts since entering bankruptcy two years ago. The company also stopped making premium payments to the PBGC to cover $8.3 billion in pension liabilities for plans covering 120,000 workers and retirees.<p>
Now United wants even further cuts worth $2 billion--including $191.1 million from pilots, $137.6 million from flight attendants, $101.2 million from mechanics and airplane cleaners and $180 million from ticket agents, bag handlers and customer service representatives. Management wants a permanent pay cut of 5 percent and another 4 percent temporary wage reduction that would last for six months after United leaves bankruptcy.<p>
But there's a growing sense among workers at United that there's nothing left to give back. "I don't care if you pay us $80 an hour," said Richard Turk, a mechanic and communications officer for Aircraft Mechanics Fraternal Association (AMFA) Local 9. "If only eight of us are left working, it really doesn't do any good."<p>
Turk isn't exaggerating much. United wants virtually unlimited outsourcing of mechanics' work. "In some cases, existing United employees will simply change uniforms, but receive lower pay and benefits," the <i>Rocky Mountain News </i>reported.<p>
AMFA, which is negotiating its first contract with United following the ouster of the International Association of Machinists, will, like the flight attendants, face a decision on whether to strike within weeks. <p>
A bankruptcy court has set a January 10 hearing on United's attempt to void its contracts. <p>
A similar threat at US Airways prompted an 86 percent vote to authorize a strike by customer service representatives. However, the workers' union, the CWA, is backing a tentative agreement for a seven-year contract that cuts wages and freezes them for three years, reduces the number of holidays and drastically limits overtime pay. Some of these concessions will "snap back" to 1999 levels--but not until January 1, 2012.<p>
Voting on this deal will be completed around Christmas. If workers vote no, they could yet go on strike.<p>
AFA's Pat Friend is right. It's high time for airline unions to draw the line--and be ready to shut down the entire industry if these drastic cuts are imposed.<p>

