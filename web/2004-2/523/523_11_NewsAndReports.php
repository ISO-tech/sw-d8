<font face="Times New Roman, Times, serif" size="4"><b>We say no to war and occupation</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--More than 130 union members, officials and labor activists from across the country met here December 3-4 to continue the work of U.S. Labor Against the War (USLAW). The meeting, a national leadership conference, was aimed primarily at raising funds and creating a structure to continue antiwar organizing in the labor movement.<p>
USLAW, like the rest of the antiwar and labor movements, had a low profile for much of 2004 as its key activists involved themselves in campaigning for the Democratic Party. Unfortunately, relatively little time on the agenda was given to assessment.<p>
There were some important debates--such as whether to support the Iraqi Federation of Trade Unions, which is closely tied to the Iraqi Communist Party, a part of Washington's puppet Iraqi government. Workshop sessions focused on specific tasks, such as advancing the debate on foreign policy within the labor movement ahead of July's AFL-CIO convention, and work with military families.<p>
The weekend began with a public meeting, sponsored by Chicago Labor Against the War, which featured speakers on the struggle to prevent the U.S. Navy from turning the city's Senn High School into a naval academy. More than 60 people turned out to hear speakers that included Senn teacher and Chicago Teachers Union activist Jesse Sharkey; Senn student Brian Meza; Chuck Hutchcraft of American Friends Service Committee; Dace Kezbers of Military Families Speak Out; and Bill Davis, president of International Association of Machinists Local 701 and a national coordinator of Vietnam Veterans Against the War.<p>
-- In <b>Boston, </b>more than 400 students from a dozen area schools converged on the Boston Common December 4 to demand that the Bush administration "Bring the Troops Home Now!" The rally featured historian and activist Howard Zinn, who reminded the crowd that even the anti-Vietnam War movement started small and eventually grew to "100 times this size."<p>
The coalition, Boston Student Antiwar Mobilization, is shifting its focus towards protesting Bush's inauguration on January 20 in Washington, D.C.<p>
<i>Lee Sustar and Joe Knott contributed to this report.</i> <p>
