<font face="Times New Roman, Times, serif" size="5"><b>Behind baseball's steroids uproar</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Dave Zirin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 10, 2004 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">MAJOR LEAGUE Baseball is in the grip of steroid hysteria. In testimony before a grand jury investigating the Bay Area Lab Cooperative (BALCO) and leaked to <i>San Francisco Chronicle</i>, New York Yankees star Jason Giambi and National League Most Valuable Player Barry Bonds of the San Francisco Giants admitted to using steroids (although Bonds says he was unaware at the time.)<p>
One drug that Giambi admitted ingesting is a female fertility drug called Clomid, which some medical experts say exacerbates pituitary tumors--something Giambi suffers from.<p>
Steroids and their link to increased power hitting appear to be a fact of life in baseball's recent history. Only 17 times has a player hit 56 or more home runs in a single season. Eleven of those seasons came between 1997 and 2001. <p>
The moon-shots were epic, and Major League Baseball loved every minute of it. It was Major League Baseball that rode the 1998 home run battle between Mark McGwire and Sammy Sosa--commonly known as "the home run race that saved the game"--to a returned popularity not seen since before the lockout/strike season of 1994. It was Major League Baseball that approved Nike's sexist "Chicks Dig the Long Ball" ad campaign. And it was Major League Baseball that rewarded the big hitters with eye-popping contracts.<p>
Baseball Commissioner Bud Selig scurried to point fingers at the players' union as the cause of steroid abuse--because the union fought Selig's plans for strict unilateral testing. Selig wants to use the scandal to turn the tables on the union he abhors.<p>
Yet the brunt of the attacks, as Selig has signaled, will be aimed directly at the players. The union has been attacked, slandered and even brought in front of Sen. John McCain's (R-Ariz.) Commerce Committee for not marching in lockstep with the owners' draconian testing proposal. <p>
The union believes quite correctly that unless testing is done impartially--in other words, not operated by Major League Baseball--the owners will use this power to request blood and urine samples as a method to void burdensome contracts. This is exactly what the Yankees are doing now to Giambi in an attempt to save $80 million. <p>
As long as baseball pays big money to the big bashers and glorifies the long ball, drugs will be ingested--and as long as players are pressured by agents and management to keep up with the guy at the next locker, there will be more Giambis to come. <p>
That's not the union's problem, or even the players' problem. It's caused by owners who see players as pieces of equipment, easily disposed of and easily replaced.<p>
