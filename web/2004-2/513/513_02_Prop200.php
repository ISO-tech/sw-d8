<P><font face="Times New Roman, Times, serif" size="4">Anti-immigrant forces push Arizona initiative</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Say no to Prop 200!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jeff Bale</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 24, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ANTI-IMMIGRANT forces have shifted into high gear in Arizona. The right-wingers are pushing hard to get support for Proposition 200, the misnamed "Protect Arizona Now" ballot initiative. Similar to Prop 187 in California several years ago, Prop 200 would require Arizona residents to prove their U.S. citizenship before registering to vote or applying for government services.</P>
<P>What makes this initiative especially draconian is that it would force government workers to be the front line of immigration law enforcement. If state employees don't report an undocumented immigrant trying to get services like welfare or food stamps, they--<I>the employee</I>--would face arrest and prosecution.</P>
<P>Supporters of Prop 200 have tried to distance themselves from the blatant racism behind the measure. Instead, they say its provisions will prevent voter and welfare "fraud." Yet study after study shows that undocumented workers put many times more money into the economy in services performed and taxes paid than they take out. </P>
<P>What's more, Arizona falls close to the bottom among the 50 states in terms of aid to the poor. In other words, no undocumented immigrant is going to rise their life trekking across the Sonoran Desert just to sign up for Arizona's measly benefits.</P>
<P>Of course, there<I> is</I> plenty of vote fraud in Arizona. But it's not being committed by immigrants--documented or otherwise. While soliciting signatures to get Prop 200 on the ballot, supporters conveniently forgot to tell people about the strict penalties for government workers who don't rat out immigrants. What's more, Prop 200 was thought to be headed for defeat until groups from outside Arizona--in particular, the right wing Federation for Immigration Reform--pumped money and resources into the effort.</P>
<P>Even more fraudulent is the outright disenfranchisement campaign led by Arizona's Democratic Party establishment to challenge Ralph Nader's right to be on the ballot. As a result, Nader is in court <I>just to get on the ballot</I>, instead of being out on the streets getting support.</P>
<P>Last week, a pro-immigration coalition announced its formation with a splashy press conference in downtown Phoenix. Composed of church and Latino rights groups and unions, the group vowed to go all out to defeat Prop 200. </P>
<P>Most compellingly, the firefighters' union in Arizona signed on. They are worried that the definition of "government services" is so broad in Prop 2000 that firefighters could face stiff sanctions for helping undocumented immigrants simply by answering a 911 call.</P>
<P>Unfortunately, the anti-Prop 200 coalition has given up ground to the right by conceding that there is a "problem" with immigration in the state. They even call themselves "Arizonans for Real Immigration Reform." With such muddled resistance to the measure, it is hardly a surprise that 66 percent of Arizonans support Prop 200--and stunningly, 59 percent support the stiff penalties against government employees who don't turn in undocumented immigrants.</P>
<P>Yet building a fightback against such a backward initiative shouldn't be that hard. Even John McCain, Arizona's veteran Republican senator, came out against the measure this week. What we need is a campaign that calls out the racists for what they are--and builds action throughout the state to defeat not only the referendum, but the right-wingers who stand behind it.</P>


