<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Washington's occupation of Iraq spirals out of control</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Has the U.S. already lost?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 24, 2004 | Page 3</font></P>

<P><font face="Times New Roman, Times, serif" size="3">GEORGE W. Bush has never let reality stand in the way of proclaiming victory in Iraq. More than a year after declaring "mission accomplished," Bush is telling campaign audiences that "freedom is on the march" in Iraq--at precisely the moment that everyone else is saying the U.S. occupation is spinning out of control.</P>
<P>"The bottom line is, at this moment, we are losing the war," said Andrew Bacevich, a former Army colonel and a professor at Boston University. "That doesn't mean it is lost, but we are losing, and as an observer, it is difficult for me to see that either the civilian leadership or the military leadership has any plausible idea on how to turn this around."</P>
<P>Insurgents control at least three dozen cities and towns across Iraq, which they use as a base of operations to mount an average of 75 attacks a day on U.S. forces--an increase of several hundred percent in just the past few months.</P>
<P>Last week, a National Intelligence Estimate report produced for Bush in July was leaked to the press, confirming that any honest reckoning of the situation paints a bleak picture. At best, according to the classified document, Iraq may enter a period of fragile stability. At worst, it could plunge into an outright civil war.</P>
<P>Washington's claim that Iraqis would greet U.S. soldiers as "liberators" was always a fraud. But it is clearer than ever that American forces are seen as "occupiers." "I see no ray of light on the horizon at all," Jeffrey Record, professor of strategy at the Air War College, told <I>Salon </I>reporter Sidney Blumenthal. "The worst case has become true...I see no exit. </P>
<P>"We've been down that road before. It's called Vietnamisation. The idea that we're going to have an Iraqi force trained to defeat an enemy we can't defeat stretches the imagination. They will be tainted by their very association with the foreign occupier."</P>
<P>With the presidential election looming, the Bush administration is desperate to keep U.S. troops as much out of harm's way as possible. So it has continued with its fruitless efforts at creating an Iraqi military force, while using its warplanes to drop bombs on heavily populated areas from a safe distance.</P>
<P>But even some Republicans are joining the chorus of criticism. Sen. Chuck Hagel (R-Neb.) lashed out at Bush's plan to shift resources from reconstruction to security: "It's beyond pitiful, it's beyond embarrassing. It is now in the zone of dangerous."</P>
<P>But as bad as the situation is in Iraq now, it's bound to get worse. That's because if the U.S. wants the national Iraqi elections scheduled for January to go ahead as planned, it will have to mount an offensive against the insurgency in Falluja and other towns in the rebels' hands.</P>
<P>Without an offensive, U.S. and Iraqi officials would be unable to carry out preparations for the vote in strongholds of the insurgency, robbing the elections of any hope of legitimacy and setting back the U.S. effort to put a credible Iraqi face on its occupation. "[A] decision has been made" to attack Falluja "after the first Tuesday in November," retired Gen. Joseph Hoare told Blumenthal. "That's the cynical part of it--after the election. The signs are all there."</P>
<P>But an all-out on attack on Falluja would be playing with fire--risking an even greater upsurge of resistance. It may already be too late to win the support of the region's Sunnis. The Association of Muslim Scholars, Iraq's largest group of Sunni clerics representing some 3,000 mosques, has already announced that it won't take part in the elections.</P>
<P>Some in the antiwar movement still argue that the U.S. has a responsibility to stay in Iraq and help rebuild. The U.S. owes a debt to the Iraqi people, goes the argument, so it wouldn't be right to "cut and run." But the very goals that the U.S. is pursuing are driving Iraq into further chaos.</P>
<P>For example, the U.S. wants the elections to produce a pro-occupation puppet government--and fast, so that it would lend greater legitimacy to U.S. counterinsurgency efforts. But this is bound to produce a backlash. "Bad elections will open wounds rather than heal them," said Ghassan al-Atiyyah, director of the Iraqi Foundation for Development and Democracy. </P>
<P>If the U.S. stays in Iraq, it will only make the situation worse. Washington wants to continue its occupation not to rebuild the country, but to set up military bases and exploit Iraq's oil resources.</P>
<P>The antiwar movement shouldn't put a progressive gloss on Washington's imperial aims. We have to call for an immediate end to the U.S. occupation--and support the right of Iraqis to determine their future.</P>


