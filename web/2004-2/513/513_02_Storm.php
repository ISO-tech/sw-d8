<P><font face="Times New Roman, Times, serif" size="5"><b>Left behind to face the storm</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 24, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ALMOST ENTIRELY overlooked in the news about the destruction caused by Hurricane Ivan last week was the manmade disaster waiting to happen. As tens of thousands of people in Louisiana, Mississippi, Alabama and Florida fled their homes, thousands more--mostly poor and Black--wanted to escape, but couldn't. As left-wing author Mike Davis commented, "Affluent white people fled the Big Easy in their SUVs, while the old and car-less--mainly Black--were left behind in their below-sea-level shotgun shacks and aging tenements to face the watery wrath.</P>
<P>"New Orleans had spent decades preparing for inevitable submersion by the storm surge of a class-five hurricane. Civil defense officials conceded they had 10,000 body bags on hand to deal with the worst-case scenario. But no one seemed to have bothered to devise a plan to evacuate the city's poorest or most infirm residents."</P>
<P>That included people like Ernestine Dees, who told the New Orleans <I>Times Picayune</I> that she would have been the first one to leave town if she could have. "We don't have any money to leave and no car," she said. "But if I could get out of here, I'd be gone in a heartbeat." </P>
<P>It's not as if officials didn't know that thousands would need emergency shelter and supplies. In 1998, during Hurricane Georges, more than 14,000 people showed up when officials finally opened the New Orleans Superdome to those who needed refuge. Not surprisingly, most were without basic supplies--and the city was totally unprepared, feeding people only hot dogs. </P>
<P>This time, says Davis, "Only at the last moment, with winds churning Lake Pontchartrain, did Mayor Ray Nagin reluctantly open the Louisiana Superdome and a few schools to desperate residents. "He was reportedly worried that lower-class refugees might damage or graffiti the Superdome."</P>
<P>While the hurricane didn't hit New Orleans as hard as expected, people are left wondering what might happen the next time. "Over the last generation, City Hall and its entourage of powerful developers have relentlessly attempted to push the poorest segment of the population--blamed for the city's high crime rates--across the Mississippi River," says Davis.</P>
<P>"Historic Black public-housing projects have been razed to make room for upper-income townhouses and a Wal-Mart. In other housing projects, residents are routinely evicted for offenses as trivial as their children's curfew violations. The ultimate goal seems to be a tourist theme-park New Orleans--one big Garden District--with chronic poverty hidden away in bayous, trailer parks and prisons outside the city limits."</P>
<P>All of which means disaster for poor people caught out in the next big storm.</P>


