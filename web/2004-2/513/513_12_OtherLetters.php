<p><a name="Dirty"></a><font face="Times New Roman, Times, serif" size="4"><b>Don't support dirty tricks</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Your article on the Vermont Green Party ("Vermont Greens support Nader," September 17) glorifies the attempts of the Vermont and Utah state Green Parties to remove the democratically decided presidential and vice presidential candidates, David Cobb and Pat LaMarche, from the ballot.</P>
<P>What you don't mention is that the move to remove Cobb from the ballot in Utah was made by a minority of six, including the official state liaison to the secretary of state. They were rebuked and removed from leadership by a wide majority at an emergency convention of the state party, but it was too late to undo the damage as the deadline to get Cobb on the ballot had passed during the internal wrangling. </P>
<P>Considering that Nader is arguing that he has the right to appear on the Reform Party ballot in Florida and Michigan due to the party's national endorsement--despite the opposition of the local Reform state leaders to his candidacy and his own problems with Democratic Party operatives using underhanded moves to keep his name off the ballot--the hypocrisy in his and your support to keeping Cobb-LarMarche off the ballot through dirty tricks is breathtaking. <BR>
<B>Alex Hogan,</B> D.C. Statehood Green Party, Washington, D.C.</P>

