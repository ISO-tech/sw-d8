<P><font face="Times New Roman, Times, serif" size="4"><b>Nader alone isn't enough</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
I'm interested in the articles <I>SW</I> writes, and I agree that capitalism is the problem in our society, but I can't seem to understand how Nader fits the mold for a real change. </P>
<P>I understand the need to utilize existing tools, such as Nader and his reformist ideas, to introduce more people to revolutionary ideas, but I don't understand why <I>SW</I> would give a reformist candidate complete support. There should be agreement with the reformists' ideas as well as critique of why Nader and his ideas are not enough.<BR>
<B>Ramsey,</B> from the Internet</P>

