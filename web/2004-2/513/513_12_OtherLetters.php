<p><a name="Martha"></a><font face="Times New Roman, Times, serif" size="4"><b>Martha tries to remake image</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Martha Stewart, the corporate criminal who has been convicted on SEC violations and sentenced to a whopping five months in prison has the nerve to compare her "oppression" with that of anti-apartheid activist Nelson Mandela. In the ABC News interview, Stewart said, "I'm a really good camper. I can sleep on the ground. There are many, many good people who have gone to prison. Look at Nelson Mandela."  </P>
<P>Nelson Mandela and Martha Stewart should never be used in the same sentence. Mandela spent years in prison for his political activism against the racist apartheid in South Africa. His group, the African National Congress, was labeled a terrorist organization under the "gone but not forgiven" Reagan administration in the 1980's.  </P>
<P>Stewart on the other hand faces a "hard" five months of prison. What a joke. People in California can get 25-to-life for stealing a piece of gum under the "three strikes" law, and she complains she gets five months in prison for stealing thousands if not millions of dollars in illegal stock exchanges?  </P>
<P>This proves that the criminal injustice system is unjust. It also proves what side the media is on. When the conviction came down, Fox News, CNN and other apologists came on saying it was unexpected and too harsh of a punishment.  </P>
<P>They also allowed Stewart to plug her products as she was pleading her innocence to her fans. We cannot allow corporate criminals to compare themselves to people like Mandela or any other populist they might think is in fashion. <BR>
<B>Matias Marin,</B> Riverside, Calif.</P>

