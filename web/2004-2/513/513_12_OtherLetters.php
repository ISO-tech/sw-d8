<p><a name="PETA"></a><font face="Times New Roman, Times, serif" size="4"><b>Ethical treatment of humans?</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</I>,</B> <BR>
On July 21, Pilgrim's Pride, a poultry supplier for Kentucky Fried Chicken, fired 11 employees--three managers and eight hourly workers--after an undercover investigator for People for the Ethical Treatment of Animals (PETA) videotaped them abusing chickens at a plant in Moorefield, W.Va. PETA is demanding that West Virginia authorities bring criminal charges against the employees. </P>
<P>Under that state's animal cruelty laws, the employees could face prison sentences of one to three years and up to $5,000 in fines. Michael McGraw, a spokesperson for PETA, said, "In cases where workers are paid so little--and they really do have terrible jobs--they tend to take out their frustration on the animals. Modern technology can really be more humane."</P>
<P>So this justifies sending them to prison? If PETA is so concerned about suffering, is it going to take care of the families of these workers while they're in prison? For that matter, is PETA going to send out undercover investigators to see how human beings are treated in plants and factories? Are they going to send investigators to the tomato fields in Immokalee, Fla., where people are subjected to the most brutal working conditions?</P>
<P>Pilgrim's Pride President and CEO O.B. Goolsby said in a statement that employees who handle live birds must sign documents saying they understand the company's "zero-tolerance" policy for cruelty towards animals. I wonder, do the executives at Pilgrim's have to sign documents saying they understand the company's "zero tolerance" policy for cruelty towards employees?</P>
<P>Perhaps instead of PETA, we need an organization called PETP--People for the Ethical Treatment of People. The problem is that we have a system based on profit. As long as that is the case, there is going to be cruelty and suffering. Worrying exclusively about the rights of chickens is for the birds.<BR>
<B>Evan Kornfeld,</B> Los Angeles</P>

