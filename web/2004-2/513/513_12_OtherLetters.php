<p><a name="Black"></a><font face="Times New Roman, Times, serif" size="4"><b>They can't have the Man in Black</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
One part of the recent Republican National Convention in New York City had me so angry I was ready to spit nails. Among the multitude of swanky multi-million-dollar parties thrown by corporations for the purpose of buying favors from politicians was one that was particularly loathsome.</P>
<P>The American Gas Association--a network of 154 utility multinationals--hosted an exclusive "celebration" of deceased singer Johnny Cash for the Republican delegation from Tennessee at snooty Sotheby's auction house. Are the Republicans so ignorant and arrogant that they think they can get away with trying to claim Cash as one of their own?</P>
<P>The "Man in Black" who sang for the poor and oppressed, advocated for prisoners' rights and spoke out against the Vietnam War might have a thing or two to say today about the Bush gang "celebrating" him today. After all, this is a man who sang about wearing: "Black for the poor and the beaten down/Livin' in the hopeless, hungry side of town/I wear it for the prisoner who has long paid for his crime/But is there because he's a victim of the times."</P>
<P>Thankfully, the "Man-in-Black Bloc" and the "Guitar and Pompadour Bloc" got together to give the Republicans who slithered over to Sotheby's a proper Johnny Cash welcome. As they said on their Web site, "We think that Johnny wouldn't be too keen on this, given the beating the current administration has been giving to our nation's poor. </P>
<P>"Actually, he might have been downright pissed that the Republicans have linked his name to theirs."  Johnny Cash was on our side. Don't let the Republicans tell you otherwise.<BR>
<B>Nicole Colson,</B> Chicago</P>

