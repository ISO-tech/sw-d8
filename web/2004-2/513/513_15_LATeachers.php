<P><font face="Times New Roman, Times, serif" size="4">Rank-and-file activists pose election challenge in UTLA</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Dissent in LA teachers union</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs, United Teachers Los Angeles</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 24, 2004 | Page 15</font></P>

<P><font face="Times New Roman, Times, serif" size="3">LOS ANGELES teachers have been working without a contract for over 14 months while our bosses at the Los Angeles Unified School District (LAUSD) drag their feet in negotiations. We've been waiting over 30 years for the LAUSD to build new high schools in the impoverished communities of South Central and East LA. We've watched the LAUSD bureaucracy quadruple in size.</P>
<P>Meanwhile teachers and students face larger class sizes and more standardized tests. Teachers and students are under attack, yet our union leaders have no strategy for fighting back.</P>
<P>John Perez, president of United Teachers Los Angeles (UTLA), is pulling out all the stops to help elect John Kerry and John Edwards. But the crisis facing our union won't be solved in the presidential elections. Both Kerry and Edwards voted for George W. Bush's anti-teacher, union-busting No Child Left Behind Act.</P>
<P>Worse, Kerry's campaign has proposed an education plan that would impose so-called merit pay and attack teachers' seniority rights. In fact, UTLA nearly went on strike over those exact same issues in 2000 when they were pushed by the LAUSD.</P>
<P>Rather than organize a challenge to the school board, however, Perez used a recent union conference to advocate a "partnership with administration in improving education." Is he talking about "partnership" with the same district that spent most of last year threatening its employees with $60 million in health care cuts?</P>
<P>In fact, UTLA leaders spent much of last year trying to convince us to consider what kinds of health care cuts we would prefer. The overwhelming response from UTLA members was, "Hell no!" Hundreds of teachers protested at a May 25 school board meeting in a rally organized by rank-and-file UTLA members.</P>
<P>A week later, LAUSD backed off of its demand for health care cuts. After months of doing nothing to mobilize teachers to defend health care, and refusing to support the May 25 rally until the last minute, Perez now wants to take credit for the health care victory. But we stopped the health care cuts in spite of Perez's leadership, not because of it.</P>
<P>The May 25 rally would not have happened without the organizational backbone provided by members of Progressive Educators for Action (PEAC). PEAC is a dissident organization of UTLA members dedicated to union activism, grassroots democracy and social justice.</P>
<P>Now PEAC is allying with other UTLA activists to run an opposition slate, called United Action, for next year's union elections. United Action will contend for at least 80 percent of the seats on UTLA's Board of Directors and three out of seven citywide offices. </P>
<P>At the same conference where Perez spoke of "partnership" and "cooperation" and the school board, United Action held its own meeting. "We cannot afford our union leaders to be cheerleaders for administration," said Julie Washington, United Action's candidate for Elementary Vice President. "We need a leadership that will step to them and say this is not good enough!  We demand respect!"</P>
<P>Electing people like Julie and the rest of the United Action slate will be an important step forward for UTLA. At the same time, we must continue to build a rank-and-file network of union militants ready to reorganize the union from the school sites up.</P>
<P>Recognizing this, PEAC is sponsoring a "School Site Organizing Workshop" on September 30. Not only will a base of member activism be needed to back up United Action, but as the May 25 rally showed, true union power lies with the rank and file.</P>

