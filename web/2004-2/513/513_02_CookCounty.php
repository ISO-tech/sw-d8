<P><font face="Times New Roman, Times, serif" size="4">Grand jury investigation of the cover-up</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Mass beating in Cook County jail</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Kyle Gilbertson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 24, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">A SPECIAL grand jury investigating the mass beating of prisoners at Chicago's Cook County Jail in 1999 issued a report charging that responsibility runs straight to the top. The grand jury concluded that the guards who carried out the assault on dozens of prisoners in February 1999 should have been charged with a crime--but that the statute of limitations had run out. "The investigation...sat dormant for almost two years until there was no realistic chance of bringing criminal charges," read the report.</P>
<P>More politically explosive is the jury's conclusion that high-level officials in the Cook County Sheriff's Department covered up the beating and later investigations. As for Sheriff Michael Sheahan himself, the report accuses him of deliberately ignoring the scandal so he could keep his hands clean. "I feel somewhat vindicated by the findings, but they're not binding," one of the victims, Cello Pettiford, told <I>Socialist Worker. </I>"They shouldn't stop short with the grand jury findings. I'll feel better when they hand down some indictments."</P>
<P>The story of what happened in Cook County Jail on February 24, 1999 recalls all the cruelty of the torture at Abu Ghraib Prison in Iraq. As many as 40 officers of the Special Operations Response Team (SORT) raided a tier at the jail's Division IX, forcing 49 prisoners to strip naked and threatening them with unmuzzled guard dogs.</P>
<P>The guards then systematically beat and stomped the prisoners. Pettiford was beaten so badly that he urinated on himself. He was forced to recite obscenities, including the phrase "SORT runs this motherfucker."</P>
<P>According to a complaint filed by Pettiford, squad leader Remus climbed on a table and shouted, "I get paid whether I come here or not. You can get paid when I leave, because I don't give a damn about lawsuits. We're going to kick your ass every time we have to come here." Pettiford had a seizure and lost consciousness. Belatedly, he was taken to the hospital, where he was diagnosed with blunt head trauma and a concussion. </P>
<P>Jail personnel ignored specific instructions from paramedics, and kept Pettiford and many others from receiving medical treatment. When he filed a grievance regarding the incident and the denial of medical treatment, guards retaliated by beating and choking him in his cell.</P>
<P>Though the grand jury cleared guards of allegations of a similar beating in 2000, its report concluded that the SORT officers were "a force unto themselves." The panel didn't accuse Sheriff Sheahan of participating in the cover-up of the 1999 assault, charging only that he "engaged in a not-too-skillful exercise of deliberate ignorance, even as his people failed miserably in their responsibilities."</P>
<P>Sheahan's cousin, James Ryan, who Sheahan appointed chief of operations of the department, didn't get off so easy. According to two internal police memos, Ryan ordered a halt in the investigation into the beatings after Charles Holman, a sheriff's department internal affairs officer, produced a report supporting the prisoners' allegations. </P>
<P>Ryan, who is running for Cook County judge in the November elections, originally denied any knowledge of the memos. He later conceded that he may have requested that the investigation be transferred to a different office, but doesn't recall doing so.</P>
<P>The special grand jury in this investigation can't convict anyone. It can only pass its findings on to prosecutors--including Cook County State's Attorney Dick Devine, who is already the target of activists' anger for dragging his feet in investigating and prosecuting Chicago police who tortured Black suspects. "I believe the whole sheriff's department is corrupt," said Pettiford. "And officials cover up until the statute of limitations runs out. That's why the law needs to be changed--to hold these officials accountable."</P>
<P>Without high profile reporting by the <I>Chicago Tribune, </I>this investigation would never have gone as far as it has. We can't rely on police and prison guards to investigate themselves. We have to organize--and make these criminals pay for their crimes. </P>


