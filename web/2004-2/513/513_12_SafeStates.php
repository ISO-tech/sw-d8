<P><font face="Times New Roman, Times, serif" size="5"><b>Will a safe-states strategy build the left?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 24, 2004 | Page 12</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
William Joseph Anderson wrote to <I>SW </I>(September 17) to "take serious issue with the way <I>Socialist Worker</I> has been misrepresenting and lying about both the Green Party and David Cobb over the past few months." William's main point of contention is that <I>SW</I> has reported that Cobb is running a "safe states" campaign, only looking for votes in states where the outcome of the election is certain, in order to not take votes from John Kerry where it actually matters.</P>
<P>William maintains that this is a distortion of Cobb's and the Green Party's position. Respectfully, I would submit that William is engaged in some serious wishful thinking regarding the Cobb campaign. I would like to direct him to the Vote Cobb campaign Web site.</P>
<P>From Cobb's own FAQ section (<a href="http://www.votecobb.org/faq#6" target="_blank">www.votecobb.org/faq#6</a>):</P>
<P>Question: "Didn't the Green Party cost Gore the election in 2000? Aren't you afraid of doing that to Kerry?"</P>
<P>Answer: "David is running more heavily in states where the outcome of the vote is predictable, based on history. Green voters in those states can choose David without worrying that they are contributing to returning Bush to the White House."</P>
<P>Question: "Does David want to help John Kerry get elected President?"</P>
<P>Answer: "No, but he also doesn't want to help re-elect George Bush. We plan to direct our campaign resources in ways that build the Green Party the most without helping George Bush."</P>
<P>Most telling is that in the first answer, the word "predictable" is a link that can be clicked on--taking you to Ted Glick's July 1, 2004 article titled, "<a href="http://www.zmag.org/sustainers/content/2003-07/01glick.cfm" target="_blank">A Green Party 'Safe States' Strategy</a>," where he argues, "The best way to do all of these things is to explicitly focus the campaign only in those 'safe states' where past voting histories and current polling indicates that either Bush or the Democrat is very likely to win."</P>
<P>This makes it clear that Cobb has positioned the Green Party to not take both major parties head-on as they did in 2000. I believe this is a tragic mistake that will set back the project of building a third party in this country for years to come.</P>
<P>Progressives and leftists are at a historic moment, and despite the dime's worth of difference between Bush and Kerry, we are faced with a stark choice. On the one hand, we can continue to pretend that a strategic reliance on the Democrats will help us stop the rampaging right in this country (despite decades of evidence to the contrary), thereby continuing to feed illusions in the "choice" offered by this corrupt, undemocratic, two-party system. This is what a "safe states" strategy does, implicitly or explicitly.</P>
<P>On the other hand, we can stand as a resolute minority (for now) that votes and organizes for what we really believe in. We can build a left that understands that lowering our expectations today always lowers our outcomes tomorrow. We can use this election to build the kind of fighting confidence and independence from the two-faced Democrats that a new generation of activists need for the struggles ahead.</P>
<P>We can accomplish these goals even in this grim election year by rejecting lesser evilism whether it comes into our movement through the backdoor or the front, and by voting for the only candidates who seem to understand this historical imperative: Nader and Camejo.<BR>
<B>Kirstin Roberts, </B>Chicago</P>

