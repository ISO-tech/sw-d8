<P><font face="Times New Roman, Times, serif" size="5"><b>If the stolen election ended in revolt</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Snehal Shingavi</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 24, 2004 | Page 13</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2">Aaron McGruder and Reginald Hudlin, <B><I>Birth of a Nation, </B></I>illustrated by Kyle Baker, Crown, 2004, 144 pages, $25.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">IF THERE had been an uprising of African Americans after their disenfranchisement in the 2000 elections, the world might look substantially different. This is the premise of "Boondocks" comic creator Aaron McGruder and filmmaker Reginald Hudlin's graphic novel, <I>Birth of a Nation</I>--the story of East St. Louis's revolution.</P>
<P>Before the 2000 elections, East St. Louis is all the things that St. Louis is not--an "inner city without an outer city" with high infant mortality rates and plagued by corruption. So, when thousands of African Americans, including the mayor, are turned away from the polls, the city has more than exceeded its breaking point.</P>
<P>Events in the novel proceed quickly from there. An opportunist billionaire proposes seceding from the union, and the idealistic mayor puts into play events that begin to force the people of East St. Louis into the fight of their lives.</P>
<P>Governor Cauldwell of Texas, the new president and a thinly veiled caricature of George W. Bush, attempts to crush the rebellion, first by freezing their assets, then by waging a covert CIA operation, and then finally by invading. In the meantime, the people of East St. Louis, now renamed Blackland, have problems of their own: publicly funded hospitals threaten to close down, a population that depends on government money finds their services canceled, and the U.S. has cut off the city's power.</P>
<P>At each step, though, people are forced to choose sides. Racism in the U.S. military forces a patriotic pilot to join the rebellion in Blackland. In major cities across the country, protests break out in support of Blackland's rebellion. <I>Birth of a Nation</I> is an inspiring novel at a time when politics have become woefully uninspired.</P>
<P>It does suffer, though, from some unfortunately tolerated sexism and some glaring plot holes. Still, this novel argues that a different world is possible, and ours for the making--and that makes it a fabulous read.</P>

