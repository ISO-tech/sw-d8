<font face="Times New Roman, Times, serif" size="5"><b>How low will they go?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 24, 2004 | Page 1</font></P>

<P><font face="Times New Roman, Times, serif" size="3">DIRTY TRICKS and empty rhetoric have made the 2004 presidential election one of the most bankrupt that anyone can remember. The Republican attack machine is revved up on overdrive--from the "Swift Boat Veterans for Truth" slandering John Kerry's service in Vietnam to television ads implying that the Democrats were responsible for the September 11 attacks.</P>
<P>George Bush's creepy campaign brain trust knows they have to distract attention from the disastrous invasion of Iraq and an economy that has lost more than 1 million jobs since their boss took office. And there's nothing they won't do to win.</P>
<P>But the Democrats and their candidate John Kerry can't--and won't--offer a real alternative. Kerry's campaign has been utterly vacant--offering nothing of substance to the millions and millions of people who hate George Bush and everything he stands for.</P>
<P>If the Democrats are taking a pounding from the right-wing attack dogs, they have only themselves to blame. They invited a campaign fought mainly over the issue of who would be tougher in "defending" the U.S.--by turning the Democratic National Convention in Boston into a nauseating display that relentlessly trumpeted Kerry's credentials as a war hero at the expense of everything else.</P>
<P>Bush's only response to the deepening crisis of the occupation of Iraq is to repeat that the U.S. can't "cut and run." And Kerry has agreed--vowing to continue the occupation throughout his hypothetical first term in office.</P>
<P>The Kerry campaign recently promised that "Republican lies" won't stop the Democrats from "fighting for jobs, health care and America's security. These are the issues that really matter to the American people." But at every step of the way, their approach on the issues that "really matter" has been a pathetic "me too."</P>
<P>Kerry's only message is that he will be more "effective" than Bush--at carrying out the occupation, at prosecuting the "war on terror," at balancing the federal budget on the backs of the poor. Huge numbers of people want to defeat Bush, but all Kerry can offer on almost every issue is Bush Lite.</P>
<P>Meanwhile, the Democrats have saved their real venom not for attacking Bush and the Republicans, but for savaging Ralph Nader's pro-worker, antiwar presidential campaign. This is what you get from a political system dominated by two parties with the same goals of serving big business and promoting U.S. power around the globe--but slightly different strategies on how to pursue those goals.</P>
<P>We shouldn't settle for this. We need to organize a political alternative that fights for health care, decent jobs and an end to the occupation--no matter who sits in the White House in January.</P>


