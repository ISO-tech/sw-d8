<P><font face="Times New Roman, Times, serif" size="4">US Airways bankruptcy "a test case for breaking unions"</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>New attack on airline labor</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 24, 2004 | Page 15</font></P>

<P><font face="Times New Roman, Times, serif" size="3">AIRLINE UNIONS faced their biggest battle yet as US Airways filed for bankruptcy for the second time in two years. The company, the seventh-largest airline in the U.S., is demanding pay cuts from unions that have already made $1.9 billion in concessions over the last two years.</P>
<P>If the unions refuse, management says it will ask a federal bankruptcy judge to impose the cuts anyway. This tactic has been used at United Airlines, which got a bankruptcy judge to impose a pay cut on machinists as part of a $2.5 billion in labor givebacks to management.</P>
<P>Both airlines are using bankruptcy court to shield the companies from creditors while extracting billions from workers in a bid to stay in business--and undercut competitors in an industry plagued by overcapacity and pressure from new, low-cost carriers. "My theory is that US Airways--along with the Air Transport Association, the industry organization--is using this as a test case for breaking the unions," Rodney Ward, a laid-off flight attendant at US Airways, told <I>Socialist Worker.</P>
</I><P>Ward pointed out that Jerry Glass, a longtime anti-union industry consultant, was hired on at US Airways as the company's senior vice president of employee relations. One of Glass's main targets has been pensions.</P>
<P>US Airways last year dumped $3.7 billion in pension liabilities into the federal Pensions Benefits Guaranty Corp. (PBGC), which insures pensions but caps benefits at $44,386 per year--less than half of what some pilots expected to receive. Pilots are especially hard hit because of legally mandated retirement at age 60--and the PBGC penalizes those who retire early by limiting benefits to just $28,851 per year.</P>
<P>If the pilots search for work, they'll have to start with no seniority at the only companies that are hiring--low-cost carriers that use smaller regional jets. "If I went to work for a regional carrier, those guys are looking at around $25,000 for a first officer," John Taylor, a US Airways pilot, told the <I>Washington Post. </I>"You know, you can make more money as an assistant manager at McDonald's."</P>
<P>United Airlines--which has cut its workforce from more than 100,000 three years ago to just 62,000 today--is attempting to follow US Airways' example. The company wants the PBGC to take over $8.3 billion in pension liabilities for plans covering 120,000 United workers and retirees.</P>
<P>If US Airways and United get away with dumping pensions, other major carriers--including Delta and American--are likely to follow suit. Union leaders have tried to ride out the bad times by accepting concessions--but the relentless demands for more cuts have lead to sharp debates in the rank and file.</P>
<P>At United, the International Association of Machinists was voted out in favor of the Airline Mechanics Fraternal Association, while at US Airways, a group of pilots is vowing to hold the line on concessions. When their union, the Air Line Pilots Association, refused US Airways' latest demands for cuts, the company filed for bankruptcy for a second time.</P>
<P>The unions point out that labor costs at the airline are already lower than expected, because of concessions--and that management has blown the cash and relief from creditors that it obtained in its earlier period of bankruptcy. Driving the cutbacks is David Bronner, head of the Retirement Systems of Alabama, a $25 billion public employees pension fund that took a controlling stake in the airline in 2003.</P>
<P>Since Alabama public employees don't have collective bargaining rights, there are no union representatives on the pension fund to restrain Bronner. His plan is clear enough: Keep slashing away as long as the unions take it--and get a judge to impose the cuts if they don't.</P>
<P>The presidential elections should provide an opportunity to put the airline crisis--and the broader attacks on pensions--at the center of political debate. Instead, labor has been largely silent so as not to upstage John Kerry's conservative campaign.</P>
<P>These cuts can't be fought company by company. The enormous crisis in the airlines demands a national--and radical--response. That means restoring airline regulation now to stop the chaos--and moving on to nationalize the industry to end this destructive competition once and for all.</P>


