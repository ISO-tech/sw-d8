<P><font face="Times New Roman, Times, serif" size="4">A double standard about stealing from workers</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The boardroom robbers</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">September 24, 2004 | Page 12</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</I> <BR>
</B>The September 8 business page of the <I>Chicago Tribune</I> had two interesting stories running on top of each other. The top story announced that a mutual fund company (Invesco Fund Group) and its company sister agreed to pay $375 million to resolve litigation over trading improprieties. </P>
<P>Most of the money will be paid out to investors whom the company defrauded of their money. "I believe this sends the strongest message yet," said Colorado Attorney General Ken Salazar, "that mutual fund companies will be held accountable."</P>
<P>Below that story ran a smaller one entitled "US Airways pilots say no to proposed concessions." There, we find that US Airways pilots rejected the company's attempts to impose pay and benefit cuts, concessions adding up to $800 million.</P>
<P>Lessons can be learned, if we put these stories together, about the kind of economic system we live under and the role the state plays in it. We learn, for example, that capitalists who defraud other capitalists are punished by the state, but capitalists can squeeze money from their own employees <I>openly</I>, apparently without any fear of legal retribution. We learn that when bosses try to reduce the amount they pay to their workers in order to increase how much money they have, it is considered by all except the workers themselves a perfectly sound and reasonable business practice.</P>
<P>The same article tells us how another company, United Airlines, has announced publicly that it may completely "terminate" all employee pension plans--that is, refuse to pay back all the hundreds of millions of dollars that employees paid into a fund which the company promised to hold for the workers for their retirement. In a normal world, this would be called theft, and anyone who announced it publicly would be treated no differently than a purse snatcher who held a press conference every morning announcing how many purses he planned to steal that day.</P>
<P>A strong message is being sent out for anyone willing to connect the dots: woe to the capitalist who cheats other capitalists, but a capitalist who tries to cheat workers out of their hard-earned wages, benefits and pensions is just being a good capitalist. Companies who fleece their workers are not "held accountable" by the system. They will only be held accountable when workers organize together and begin to fight back.</P>
<B><P>Paul D'Amato</B>, Chicago</P>

