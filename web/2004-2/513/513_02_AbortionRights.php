<P><font face="Times New Roman, Times, serif" size="5"><b>Attacking abortion rights</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 24, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">AS SOON as abortion became legal with the U.S. Supreme Court's <I>Roe v. Wade </I>decision in 1973, anti-abortion forces began scheming about how to chip away at the right to choose. It's been an effective strategy--and it's been escalating, even though you might not hear about it from politicians or the mainstream media.</P>
<P>For example, earlier this month, another anti-abortion measure snuck through the House of Representatives, quietly attached to an appropriations bill. If passed, the provision would prohibit local, state or federal authorities from requiring a hospital or doctor to provide abortions or give patients abortion referrals--even if a pregnant woman had been raped or was in critical condition. Poor women who depend on federally subsidized health care and use Roman Catholic hospitals will bear the brunt of this new rule, if it goes into effect.</P>
<P>Several states have already passed similar creeping bans on abortion. In July, Mississippi passed legislation allowing health care workers and facilities to refuse to perform any service they object to on moral or religious grounds.</P>
<P>In Mississippi, South Dakota and Arkansas, pharmacists can legally withhold services on "moral grounds." "We have doctors who won't even issue birth-control prescriptions," said Nsombi Lambright of the American Civil Liberties Union's Mississippi branch. </P>
<P>And if you're a doctor who performs abortions, you better be careful, because the anti-choice fanatics may have you arrested. Last November, a law outlawing the late-term abortion procedure known as "intact dilation and extraction"--misnamed "partial-birth" abortion by the right wing--passed after years of attempts by the Christian Right.</P>
<P>The ban is worded so vaguely that it could ban several other late-term abortion procedures. On September 8, a federal judge in Nebraska became the third in recent months to rule that the Bush administration-backed law was unconstitutional--because it didn't include an exception that took into account the health of the women.</P>
<P>This is an important blow to the right wing's attempt to steam-roll over a woman's right to choose. But at the same time, abortion rights activists must be clear about what these restrictions are all about--chipping away at abortion rights as a whole.</P>
<P>We can't afford to give ground to the right-wingers on a single procedure. That's why we need to demand equal access to abortion--for every woman, in every state, for every procedure.</P>


