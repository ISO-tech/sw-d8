<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Are there any good capitalists?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 24, 2004 | Page 13</font></P>

<P><font face="Times New Roman, Times, serif" size="3">I WAS in a meeting the other day making the case for socialism to a group of city college students, and someone asked if capitalists were bad people. The short answer is expressed in the popular saying, "Nice guys finish last." But I suppose I should give a fuller answer.</P>
<P>The classic view of capitalists is the portrayal of Ebenezer Scrooge in Charles Dickens' <I>A Christmas Carol</I>. This story tells us that a capitalist can be a bad man--a wizened, grasping miser who'd sooner see his own employees starve than lose money paying them a decent wage.</P>
<P>But the story also tells us that such a man can redeem himself by acts of charity and kindness to his employees--if various and sundry ghosts frighten him enough. Versions of these "scrooge" stories get rehashed all the time in Hollywood.</P>
<P>We know the plot: a poor or working-class guy, by some act of magic, finds himself directing a company and living in a mansion, inhabiting the body of a greedy boss, and his employees are stumped because suddenly he's a do-gooder. These films satisfy our desire to see capitalism lampooned, but without actually challenging the logic of capitalism.</P>
<P>Indeed, they feed the fantasy that capitalism is an essentially benign system when in the right hands. There are evil capitalists, they tell us, but capitalism itself is not evil.</P>
<P>The top 1 percent of people in the U.S. own more wealth than the bottom 90 percent combined. Some, like Bill Gates, are worth more than several countries. According to latest estimates, he has made $51.04 per <I>second </I>since 1986.</P>
<P>The 1 percent didn't amass that kind of wealth be being "good." They amassed these fortunes by ruthlessly exploiting the majority that work for them, without any sentimentality.</P>
<P>Of course, a capitalist investor is free to feel a pang of guilt about making millions by exploiting the labor of others or by tossing "excess" workers onto the scrap heap. He can even give away something to charity and get his or her name on a building--and a nice tax break.</P>
<P>Accumulation of wealth--constantly augmenting profit--is the driving force of capitalism. It is a system based upon market competition, where competing firms attempt to minimize their costs in order to gain market share against the competition and expand their profit margin.</P>
<P>Anything that increases productivity, reduces costs and raises profits is good as far as the capitalist is concerned. Anything that raises costs, reduces productivity and cuts into profits is bad. </P>
<P>In truth, therefore, scrooge-like behavior is built into the very makeup of capitalism. Workers produce the wealth--the successful capitalist appropriates as much of it as possible. If you're a capitalist, therefore, concern for your employees is actually <I>bad for business</I>.</P>
<P>The most successful capitalists take an active interest (or delegate to others the job) in forcing wages down, increasing the length and pace of work, and slashing benefits <I>whenever they can get away with it. </I>Low unemployment, a condition in which workers are in a stronger position to get better wages, is <I>bad</I> for the capitalists; high unemployment, a condition in which workers are more willing to accept lower wages, is good.</P>
<P>If the character of a capitalist is determined by whether he tucks his kids into bed, I'm sure there are plenty of "good" ones. But their entire position in the world--their wealth and power--depends upon ruthlessly efficient exploitation, something that for workers on the receiving end appears as a very tangible evil.</P>
<P>The workers who end up with higher pay, better working conditions and improved health care for their sick kids are not the Bob Cratchits who suffer quietly and wait patiently for their employer's bad dreams to come to their rescue. "Good" behavior has to be extracted from capitalists through struggle--the only thing that really haunts them in the end.</P>


