<font face="Times New Roman, Times, serif" size="5"><b>Democrats' Obama ready to bomb Iran</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 8, 2004 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">JOHN KERRY'S antiwar supporters have repeatedly warned that a military attack on Iran is imminent if George Bush is reelected. But Democrats are rattling their sabers at the same target.<p>
On September 24, Barack Obama--the Democratic candidate for U.S. Senate from Illinois, and a shoo-in favorite--suggested "surgical missile strikes" on Iran may become necessary. "[L]aunching some missile strikes into Iran is not the optimal position for us to be in" given the ongoing war in Iraq, Obama told the <i>Chicago Tribune</i>.<p>
"On the other hand, having a radical Muslim theocracy in possession of nuclear weapons is worse," he said. Obama went on to argue that military strikes on Pakistan should not be ruled out if "violent Islamic extremists" were to "take over."<p>
A U.S. strike on Iran could well open up a new war front. When the CIA and Defense Intelligence Agency (DIA) conducted a recent series of war games involving an attack on Iran, an Air Force source told <i>Newsweek,</i> "The war games were unsuccessful at preventing the conflict from escalating." <p>
Why would Obama, whose staunch opposition to the Iraq war made him a hero among Democratic Party liberals, consider attacking Iran? Obama--a keynote speaker at the Democratic Party Convention--has a bright future in the Democratic Party. And the Democratic Party is a war party. <p>
Obama opposes immediate withdrawal from Iraq. His positions are entirely consistent with the Democratic Party's platform, which explicitly puts Iran on notice:  "[A] nuclear-armed Iran is an unacceptable risk to us and our allies...With John Kerry as commander-in-chief, we will never wait for a green light from abroad when our safety is at stake."<p>
During the first presidential debate, Kerry appeared eager to stress his willingness to "go it alone" when asked his opinion about "pre-emptive war." "The president always has the right and always has had the right for pre-emptive strike," declared Kerry, adding, "That was a great doctrine throughout the Cold War."<p>
This comment will have shocked those who recall the decades-long standoff between the U.S. and former USSR quite differently--as a period when a "first strike" by either side could easily have led to "mutual assured destruction." "Pre-emptive war" is the centerpiece of the Bush Doctrine, announced to the world after September 11.<p>
To be sure, Kerry made no fewer than 27 allusions to allies, the United Nations, summits and treaties during the debate--and continued to insist that the invasion of Iraq was a mistake. But when asked, "Are Americans now dying in Iraq for a mistake?" Kerry's answer was "No." Kerry proceeded to outline his strategy for winning, by "beginning to not back off of Falluja and other places and send the wrong message to terrorists...You've got to show you're serious." <p>
This strategy, right-wing <i>New York Times </i>columnist William Safire eagerly pointed out, "requires us to inflict and accept higher casualties." This also happens to be the strategy Bush is now pursuing in Iraq.<p>
Kerry promises to begin replacing U.S. troops with Iraqi forces next summer, with a complete U.S. pullout by the end of his first term. This strategy, known as "Vietnamization" in 1968, was the campaign slogan of Richard Nixon--denounced by the antiwar movement, John Kerry among them, when it proved to be a colossal failure.<p>
Kerry's argument that the invasion of Iraq was "the wrong war in the wrong place at the wrong time" is a sleight of hand. This is not an antiwar statement. On the contrary, it is an argument that the Iraq war was a distraction from the "real" war on terrorism--in Afghanistan, Iran, North Korea and elsewhere.<p>
Kerry's strategy is a recipe for more war--by crushing the resistance in Iraq and taking aim at other targets in the years ahead, a strategy not very different from Bush's. As Safire gloated after the debate, ""His abandoned antiwar supporters...shut their eyes to Kerry's hard-line, right-wing, unilateral, pre-election policy epiphany." The debate is not between pro-war Republicans and antiwar Democrats, but over which war in which place at which time will better advance the global aims of U.S. imperialism.<p>

