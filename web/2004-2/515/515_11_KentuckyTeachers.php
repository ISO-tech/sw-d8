<P><font face="Times New Roman, Times, serif" size="4">Fight for health care is focus of contract battle</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Teachers in Kentucky ready to strike</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Molly Seifert, Kentucky Education Association</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 8, 2004 | Page 11</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>MORE THAN 4,000 striking education workers from Northern Kentucky held a one-day strike September 27 to show their disgust and frustration with Republican Gov. Ernie Fletcher's proposed health insurance plan for 2005. Rallies took place around the state, where the strike forced 23 school districts to close for the day. </P>
<P>The strike was organized by the Kentucky Education Association (KEA) in response to the governor's new health insurance plan that will force state employees to pay higher premiums and deductibles while reducing benefits. The governor's plan is particularly outrageous in a state where teachers' salaries rank 40th in the country--yet where teachers pay the highest health insurance costs in the nation.</P>
<P>Particularly hard hit will be the educational support staff, including bus drivers, instructional assistants and custodians, who make about half as much as Kentucky teachers. Connie Gaylor, an instructional assistant from Kenton County said, "I love my job, even though I only bring home $70 per week. The reason for that low number is because I pay over $700 monthly for my family HMO--and that's under the current health plan.  Governor Fletcher's new plan (for 2005) is going to bankrupt me."</P>
<P>According to Ruth King, a Pendleton County teacher, bus drivers in her district "mostly work for the health insurance--surely not the inadequate pay.  Under Fletcher's proposed plan, many support staff will have to pay the school each week (instead of receiving a paycheck) just to get family health insurance."</P>
<P>Another support staff worker from the Covington Schools read the governor's new plan and "sat down and cried," she said. She realized that she "will have to pay $600 more per month next year for the same coverage."</P>
<P>KEA rallies throughout the state forced Gov. Fletcher to retreat by calling a special legislative session to on October 5 to craft another health insurance plan for 2005. As <I>Socialist Worker </I>went to press, hundreds of rank-and-file education workers were gathering at the state capitol for the special session to protest and continue to push for affordable health care. </P>
<P>The rank-and-file members of KEA authorized an indefinite strike to begin October 27 if the special legislative sessions does not result in a health plan that restores the current 2004 costs and benefits. As Boone County maintenance staff worker Fred Johnson put it,  "The rally on September 27 is just the beginning. If our demands are not met by October 27, then we're walking [out on strike.]"</P>

