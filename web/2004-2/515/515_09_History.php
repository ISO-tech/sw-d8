<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>What they don't teach us in school</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 8, 2004 | Page 9</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>WHY WAS history so boring in high school? It's because textbooks talked about historical events as if they were a lifeless collection of things--like a grocery list, only you had to memorize it. They left out most of the real conflict (especially class conflict) in history, presenting the "great men" of our past as flawless heroes. </P>
<P>The "founding fathers," for example, rarely got tagged as slave traders and slave masters. Real historical events like wars and revolutions were presented almost as natural disasters--things that just "happened." And the causes of various events were presented in facile, superficial ways.</P>
<P>Much of real history was simply omitted--and, in some cases, our textbooks simply engaged in outright lying. In his book <I>Lies My Teacher Told Me, </I>James Loewen surveyed 12 U.S. high school textbooks and came up with some outrageous examples. Helen Keller is presented as a deaf and blind woman who triumphed over adversity. Absolutely no mention is made of the fact that she was a lifelong socialist.</P>
<P>The Columbus myth--that he "discovered" the New World (though other people were already living there) out of "burning curiosity"--is perpetuated in almost every textbook. No mention is made of his thirst for gold, or his successful plans to kill and/or enslave Indians, conquer all their land for Spain, and steal as much gold as he could find.</P>
<P>The textbooks present the famous debates between Abraham Lincoln and Stephen Douglas debates in 1858, in which the two presidential candidates argued over the question of slavery, solely in terms of the men's look and speaking style (sound familiar?): "Douglas was what we'd call a flashy dresser," one text tells us. "He wore shirts with ruffles, fancy embroidered vests, a broad felt hat." What it doesn't mention is that Douglas, in his own words, "was opposed to taking any step that recognizes the Negro man or the Indian as the equal of the white man." </P>
<P>Only two out of the 12 books Loewen reviewed make an attempt to discuss the cause of racism in the U.S., and they do it in a single, unsatisfactory sentence. "[African Americans] looked different from members of white ethnic groups," write the authors of <I>The American Adventure </I>(note the false advertising in this title). "The color of their skin made assimilation difficult. For this reason, they remained outsiders." As Loewin points out, these passages "imply that it is natural to exclude people whose skin color is different"--i.e., it is not an explanation of racism, but a <I>justification for it</I>.</P>
<P>And there's the rub. Our history textbooks aren't written to explain anything or to help us understand the past in order to understand better our present. They are written to <I>justify</I> things <I>as they are</I>.  Those men that fit into American's national myths are heroes, and their blemishes are blurred over or removed (for example, that many our founding fathers were slave owners or slave traders). </P>
<P>On the other hand, we don't learn that John Brown was a brave freedom fighter who struck the first blow against slavery by seizing the arsenal at Harper's Ferry in 1859. We learn instead that he was a deranged "fanatic." But as Brown pointed out at his own trial before his execution, "Had I so interfered in behalf of the rich, the powerful, it would have been alright."</P>
<P>In our textbooks, history is quite simply an expression of what Marx called "the ruling ideas of society"--that is, the ideas of the ruling class. What they can't omit or deny, they <I>rationalize</I>. And they present it all as if it were the <I>revealed truth</I>, inscribed in stone somewhere, so that we'd be too intimidated to question it (or at least too bored to care.)</P>
<P>Helen Keller once attacked a Brooklyn newspaper, saying: "Socially blind and deaf, it defends an intolerable system." That perfectly describes high school history books.</P>

