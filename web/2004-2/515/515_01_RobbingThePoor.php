<font face="Times New Roman, Times, serif" size="5"><b>Robbing the poor to give to the super-rich</b></font><p>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 8, 2004 | Page 1</font></p>

<font face="Times New Roman, Times, serif" size="3">ACCORDING TO <i>Forbes </i>magazine, millionaires are getting harder to find--on their list of the 400 richest Americans, that is. Turns out that the billionaires are squeezing the piddling millionaires off the list. "With a $750 million admission price, nine-digit fortunes are an endangered species here: 78 percent of the people on this year's list are billionaires," chirped <i>Forbes.</i><p>
Topping the list, as usual, is Microsoft's Bill Gates, worth $63 billion. Members of Wal-Mart's Walton clan, who have a combined $85 billion in wealth, claimed five of the top 10 spots. Soaring oil and gas prices helped oilman T. Boone Pickens Jr.--a $500,000 contributor to the pro-Bush "Swift Boat Veterans for Truth"--move onto the list of the filthiest of the filthy rich. "The economy's recovery may be a little shaky, but you wouldn't know it from looking at this year's <i>Forbes </i>400," the magazine smirked. <p>
For this, the fat cats have George W. Bush to thank. And they had even more to be thankful for October 4, when Bush signed his fourth tax cut in four years. Hidden among a series of much-touted "middle-class tax relief" measures was a less talked-about provision--$13 billion in new corporate tax breaks. <p>
As if they needed the help--big business is already getting away with tax-break murder. According to Citizens for Tax Justice's study, 275 Fortune 500 companies paid just <i>half </i>of what they owed in 2001-2003--for a combined savings of $175 billion over three years. General Electric was at the top of the list, with $9.5 billion in tax breaks. Its tax giveaways could have paid for 212,985 full-time school teachers, or funded school breakfasts for low-income students for six years.<p>
Instead, the budget deficit caused by Bush's massive tax cuts will be paid for by cuts in programs that benefit workers and the poor. As Robert McIntyre, executive director of Citizens for Tax Justice, concluded, "If they don't pay it, somebody else has to. That means you and me."<p>
Corporate America's thank you notes will be going to the Democrats, too. On September 23, every Senate Democrat but one voted for Bush's latest tax cut package--without a complaint.<p>
John Kerry wasn't even there. And if he's elected president, he has a corporate tax break of his own to hand out--he'll reward businesses for employing workers in the U.S. and not going overseas. Kerry calls it "jobs creation." He's willing to make workers and the poor pay, too, by cutting social programs to the bone. He calls that "fiscally responsibility."<p>
So much for the "stark choice" in Election 2004. "Even on taxes--presumably the point of greatest policy difference--it's hard to tell their proposals apart," <i>Business Week </i>magazine reported. "And Kerry proposes on his Web site to reduce taxes for 98 percent of Americans, cut the corporate tax rate for most companies and completely eliminate the capital-gains tax on long-term investments in small businesses. This is hardly a clarion call for the reversal of Bush's tax cuts." <p>
No matter who wins in November, Corporate America will be laughing all the way to the bank.<p>

