<P><font face="Times New Roman, Times, serif" size="4">Is it illegal to divest?</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Targeting supporters of Palestine</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 8, 2004 | Page 12</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>PRO-ISRAEL organizations in the U.S. want to make it illegal to support the struggle for justice in Palestine. Their most recent tactic is lobbying the federal government to file charges against anyone who calls on U.S. corporations and academic institutions to divest from Israel.</P>
<P>In late September, 13 members of Congress--nine Democrats and four Republicans--signed a letter initiated by the Zionist Organization of America (ZOA) urging the U.S. Department of Commerce to shut down divestment campaigns "because they violate U.S. laws regarding the Arab boycott of Israel." The Export Administration Act of 1977 makes it illegal for U.S. citizens to participate in boycotts of nations friendly to the U.S., if the boycotts are called by other countries. Each violation carries a fine of up to $12,000.</P>
<P>The ZOA claims that the boycott of Israel by several Arab countries means that any boycott campaign in the U.S. is illegal. By the same logic, of course, the 1980s campaign for divestment from apartheid South Africa was also illegal--because other countries supported the boycott and South Africa was considered an ally of the U.S.</P>
<P>The ZOA's letter specifically targets an upcoming conference of Palestinian rights activists at Duke University, October 15-17. Pro-Israel groups have also collected more than 70,000 signatures calling on Duke University President Richard Brodhead to refuse to host the conference.</P>
<P>Some letters to the president have contained threats of violence against him personally, and against the conference. But Brodhead has so far refused to be intimidated. "Universities in particular must give wide latitude to freedom of speech," he said.</P>
<P>Fayyad Sbaihat, a spokesperson for the conference, told <I>Socialist Worker </I>that the ZOA's initiative represents growing concern that the boycott campaign against Israeli apartheid is starting to get a hearing. "They're realizing the potential of the idea of divestment," said Fayyad. "We're only in the fourth year of our campaign, and for a while, they ignored it, hoping that it would diminish by itself. But the divestment idea and campaign are gathering momentum, evident by several resolutions passed during the summer by the Presbyterian Church, the UN NGOs Conference and the Non-Aligned Movement. And, most recently, the Anglican and Episcopal Churches are debating similar resolutions. So several pro-Israeli congresspeople are trying to close down the debate by trying to claim that divestment is illegal. But our stand is that human rights are more important than free trade laws."</P>
