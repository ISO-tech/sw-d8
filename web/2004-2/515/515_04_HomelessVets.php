<font face="Times New Roman, Times, serif" size="5"><b>Ignoring a new generation of homeless vets</b></font><p>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 8, 2004 | Page 4</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</i></b><br>
My name is Charles Blythe. I'm a Navy veteran, discharged in 1965. I have operated/managed the Homeless Veteran Reintegration Programs (HVRP), funded by the Department of Labor, here in the Cincinnati area since 1993.<p>
Up until just recently, we have served combat vets of Vietnam and others from previous confrontations, such as Korea. Now, I have two combat vets home from Iraq and one from Afghanistan--homeless, suffering from post traumatic stress disorder, with little to no help from the government other than what we can give them (which I am sure is more than the government would give them).<p>
With my last funding award letter, I read a statement that went something like this from Labor Secretary Elaine Cho: "President George W. Bush and I are determined that no vet will be left behind." Yeah right! Then why do I (and others around the country) have these vets here? And why could I not get anyone interested in coming to see them or even talk about their plight while on the campaign trail?<p>
I have to ask myself when I look at homeless veterans how this country can spend tax dollars on rebuilding Iraq, housing their homeless and rebuilding their electrical grid, while there are homeless veterans here in this country. How can this administration send billions to Iraq and yet not appropriate the full amount of funds authorized for the HVRP, which is $50 million? What did we actually get to fund HVRP? $23 million.<p>
Why? How can we send billions to Iraq and can't give a mere $50 million to serve the homeless veterans here at home? Something is wrong with this picture.<p>
I remember Vietnam. We knew then something wasn't right, and now we know for sure. We were sold downstream concerning Vietnam. Are we really going to fall for it again?<br>
<b>Charles Blythe, </b>Cincinnati<p>

