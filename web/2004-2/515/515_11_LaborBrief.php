<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 8, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Legal Aid Society</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Lucy Herschel, 1199/SEIU delegate</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Members of 1199/SEIU at the Legal Aid Society voted September 27 to accept concessions proposed by the union in an attempt to avoid the layoff of 79 members. The concessions, which consist primarily of the 10-month postponement of a 3 percent raise, are intended to help fund a buyout for approximately 48 people. The layoff for the rest is only postponed for two weeks. </P>
<P>The 1199 members at Legal Aid have been fighting layoffs since the spring. An earlier attempt at layoffs was averted after a mobilization campaign. However, Legal Aid then demanded the layoff a different set of 1199 members due to technology changes. This is despite the fact that 1199 has already lost 20 percent of its membership at Legal Aid through buyouts and attrition, in just the last four months. After the next staff reductions, the union will have lost over 35 percent of its membership at the agency.</P>
<P>In August, with an 86 percent vote, 1199 members voted down a concession proposal similar to the one just passed. Yet the delegate body voted specifically not to have a public campaign.</P>
<P>The union vice president who oversees the Legal Aid chapter argued at the September 27 meeting that Legal Aid was a "dove," as opposed to other employers who are "hawks," and therefore we should be working with Legal Aid rather than fight them for our jobs. "A lot of members wanted to see a real campaign organized to fight these layoffs, publicly if necessary," Laura Duran, a homeless rights paralegal, told <I>Socialist Worker</I>. "However, when it became clear that the union leadership wasn't going to organize that fight, I think people felt they had no choice but to vote for concessions." </P>
<P>It will be up to the members to organize a fight in all the offices and force the union leadership to stop negotiating and to start fighting. </P>

