<P><font face="Times New Roman, Times, serif" size="4">Two-week strike aims to defeat health care concessions</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>SF hotel workers walk out</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Abe Gomez</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 8, 2004 | Page 11</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>SAN FRANCISCO--Some 1,400 hotel workers at four of this city's larger hotels began a two-week strike September 29 over healthcare coverage, salaries and contract length. In response to the strike, 10 other San Francisco hotels locked out workers October 1, putting another 2,600 employees on the picket line.</P>
<P>The picket line at Hilton San Francisco on the first day of picketing was well attended and the level of enthusiasm was high. Chanting "Local 2 on strike! All day and all night," angry workers marched in front of the main doors.</P>
<P>One striker, Christopher Keyes, a part-timer, underlined how important this strike was for all workers. "Losing health care is a matter of life and death," he said. "If this is what it takes, I'll have to fight to the death. I don't go to all the union meetings, but when it comes to a strike, I'm here."</P>
<P>The strike came as a result of failed negotiations between the workers' union, UNITE HERE Local 2, and the San Francisco Multi-Employers Group, which represents hotel owners. Despite posting profits of $1 billion in 2003, the "Big Four" hotel corporations are trying to squeeze workers even harder for health care and refusing to negotiate on salaries. If San Francisco hotel employers have their way, workers will have to pay $273.42 per month in health care in the fifth year of their proposed contract. Besides resisting those demands, the union is demanding a 2006 contract expiration date that would link San Francisco hotel workers' contract to those of their counterparts across the country.</P>
<P>Other UNITE HERE hotel workers--2,800 in Los Angeles and 3,500 in Washington, D.C.--have also voted to authorize strikes over similar demands, including the 2006 contract expiration date. They also could go on strike or be locked out at any time.</P>
<P>This strike is not only important for the immediate issues but also because it will set the standard for upcoming contract disputes. Support from the community was evident. Taxi drivers, truck drivers and other passersby sounded their horns in a sign of support as they drove by the picket lines.</P>
<P>Pedestrians also gave the thumbs up sign to the picketers. Reaching out to customers, other employees and passersby at the hotel with a leafleting campaign would be ways to build solidarity with the strike. It could also build the confidence and strength on the picket line that will be needed to disrupt "business as usual" at the hotel. </P>

