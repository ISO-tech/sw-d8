<P><font face="Times New Roman, Times, serif" size="4">Wife of Iraq war veteran deported</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Everything I fought for has vanished"</b></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | October 8, 2004 | Page 2</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>U.S. IMMIGRATION authorities deported Patricia Acosta Delgado, the wife of Iraq war veteran Sgt. Frank Cabadiana, in late September.</P>
<P>Last month, <I>Socialist Worker</I> printed Frank's plea for help following his wife's arrest by Miami police after authorities discovered that she had a 1998 order for her deportation. "She is my only hope to live, and since I am disabled, she's the only one able to take care of our son," wrote Frank, who was injured twice last year by grenade explosions while serving in Iraq.</P>
<P>But the government had no mercy for Frank, Patricia or their son.</P>
<P>Initially, Patricia was transferred to a detention center in New Jersey. But last month, when supporters tried to call the detention center to protest her arrest, they were told she was no longer there. That's because she had been transferred back to Florida--for a deportation hearing.</P>
<P>Citing Patricia's previous arrests for shoplifting, a judge ordered her out of the country on September 27. Unfortunately, a lawyer who was helping the couple dropped the case when authorities revealed what they described as Patricia's "lengthy" criminal record of petty charges. But as Frank told the <I>Miami Herald,</I> "We can't crucify a human being for what she did in the past. She's still my wife in good times and bad."</P>
<P>Although Patricia had entered the country illegally as a teenager, she applied for permanent resident status shortly after the couple married in 1997. That request was denied--thanks to a vicious law passed during the Clinton administration that allows the government to deport immigrants for the most minor of offenses.</P>
<P>And in a sickening error, the papers denying her residency were misdirected when the couple moved to Florida in 1998. "The lawyer put the wrong address down, and we never got them," Frank told the weekly <I>Broward New Times </I>newspaper.</P>
<P>Now, this veteran and his family are paying the price for the government's war on immigrants. "My nuclear family is gone," Frank told the <I>New Times.</I> "My dream of making a home with my family, kissing my wife, taking my son to school--everything I fought for--it vanished." He now says that he plans to take his stepson and join Patricia in Ecuador.</P>

