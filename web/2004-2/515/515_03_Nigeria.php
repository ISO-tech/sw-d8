<font face="Times New Roman, Times, serif" size="5"><b>Upheaval against a U.S. ally in Africa</b></font><p>
<p><font face="Arial, Helvetica, sans-serif" size="2">October 8, 2004 | Page 3</font></p>

<font face="Times New Roman, Times, serif" size="3">THE THREAT of armed rebellion in the oil-producing region of Nigeria cast a spotlight on the social and political crisis in that country--and Washington's cynical power plays in Africa. A last-minute truce averted an uprising led by Mujahid Dokubo-Asari of the ethnic Ijaw people, one of the indigenous groups of Nigeria's impoverished Niger Delta region that have been excluded from the country's enormous oil wealth produced on their lands.<p>
Asari's call for independence for the Ijaws followed a series of deadly government crackdowns last April and an earlier raid in August 2003. This was similar to the tactics used by Nigeria's former military dictators. In 1995, they ordered the execution of Ken Saro-Wiwa, a novelist and activist among his Ogoni people, another ethnic group in the Delta.<p>
Today's "democratic" government is little better than the old regime. Installed in 1999 and elected amid charges of fraud in 2003, President Olusegun Obasanjo has repeatedly ordered security forces into the Delta, where they have carried out systematic killings, supposedly to combat "oil smugglers."<p>
But the real oil criminals are entrenched in Nigeria's ruling class--which pockets most of the country's $100 million in daily oil revenues--and in the boardrooms at Royal-Dutch Shell, which controls half of daily production in Nigeria. Meanwhile, 70 percent of the population--some 91 million people--lives on less than $1 per day.<p>
This is the context for communal violence-- usually seen as ethnic or religious--that has killed as many as 10,000 people since 1999. "The combination of a huge pool of unemployed youth who can easily be recruited as political thugs, and the presence of thousands of millionaire politicians who use them, is a lethal combination," the BBC's Mark Doyle wrote earlier this year.<p>
To the U.S., Nigeria--the sixth-biggest producer of oil worldwide--is an ally and sub-contractor for dirty political jobs. The U.S. backed the Nigerian troops as "peacekeepers" in Liberia and Sierra Leone--where military officers became involved in diamond smuggling. Today, at Washington's urging, a Nigerian general heads a peacekeeping mission in Sudan's war-ravaged Darfour region. Washington is also enthusiastic about Nigeria's embrace of the free-market New Economic Partnership for Africa's Development, which will hand over even more of Africa's shattered economy to Western multinationals. <p>
However, workers and the poor in Nigeria have a long tradition of fighting for their own interests. The Nigerian Labor Congress (NLC) is threatening a general strike this month against an increase in fuel prices--a follow-up to a successful strike over the same issue last June. <p>
"If you surrender your country to the sheer forces of the market, you will crush the people to the dust," declared an NLC statement. In Nigeria, against the odds, the people are fighting back.<p>

