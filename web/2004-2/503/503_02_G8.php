<font face="Times New Roman, Times, serif" size="5"><b>Feds crack down for G8 meeting</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Russell Pryor and Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 11, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE POSH resort town of Sea Island in southern Georgia proved to be a fitting place for the Group of Eight summit meeting of the world's most powerful governments. After it was turned into an armed fortress, that is.</P>
<P>George W. Bush and other world leaders had arrived on the isolated 5-by-2 mile private island as <I>Socialist Worker </I>went to press. Meanwhile, thousands of protesters gathered in nearby Savannah and Brunswick in preparation for protests--defying an extraordinary crackdown by federal officials determined to silence dissent.</P>
<P>At the top of the agenda for Bush and fellow world leaders is Iraq. U.S. officials are trying to work out a deal to win international support for its fake "hand over" of power to its handpicked Iraqi government on June 30. Washington also wants other governments to cancel 80 to 90 percent of Iraq's foreign debt. Germany and France have so far been unwilling to go beyond 50 percent.</P>
<P>Six Middle East leaders--from Afghanistan, Bahrain, Jordan, Tunisia, Turkey and Yemen--accepted invitations to join a summit luncheon this week. Iraq's new "president," Ghazi Yawar, also plans to attend. But Egypt, Morocco and Saudi Arabia won't be sending representatives because of the "the whole sourness of things," according to U.S. officials.</P>
<P>The security measures for the summit will be the most extensive in U.S. history, State Department spokesperson Barry Bennett told the <I>Washington Post.</I> Weeks before the protests, Georgia Gov. Sonny Perdue declared a pre-emptive "state of emergency."</P>
<P>A few days later, the Brunswick City Council passed an ordinance allowing its police department to halt protests during "states of emergency"--giving free reign for authorities to harass and intimidate activists. At least 10,000 law enforcement officers from local, state and federal agencies are patrolling the summit area--along with the Navy and Air Force.</P>
<P>Rev. Zack Lyde, an associate pastor at St. John, told the Associated Press what he thought of the brigades of police cars and military vehicles cruising around town.</FONT><FONT FACE="Times" SIZE=5> </FONT><FONT FACE="Times">"They've been running the streets since they got here," said Lyde. "They're spreading terror like mayonnaise."</P>
<P>The stage was set for a repeat of police violence during last November's protests against the Free Trade Area of the Americas summit in Miami, when police went on a rampage. Despite this, however, protesters are coming together to take a stand against Third World debt, AIDS and poverty--as well as the occupations of Afghanistan, Iraq and Palestine.</P>
<P>An action is planned in which protesters will tear down of a wall--symbolic of the apartheid wall that Israel is currently building, with U.S. support, in the West Bank. As protesters at the 2001 summit in Genoa, Italy, chanted: "They are eight, we are six billion!"</P>


