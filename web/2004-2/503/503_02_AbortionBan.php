<P><font face="Times New Roman, Times, serif" size="4">Federal judge rules against administration</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Abortion ban overturned</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 11, 2004 | Page 2</font> </P>

<P><font face="Times New Roman, Times, serif" size="3">ABORTION RIGHTS supporters won a victory June 1 when a federal appeals court struck down the "Partial Birth Abortion Ban Act." Passed last November, the law outlaws the late-term abortion procedure known as "intact dilation and extraction"--misnamed by the right wing as "partial-birth" abortion. The procedure has been on the right's hit list for years--precisely because by banning it, they're able to take aim at other, more common, abortion procedures.</P>
<P>In her ruling, Judge Phyllis Hamilton declared the law unconstitutional because it places too much of a burden on women seeking abortions, contains vague language and lacks an exception to preserve a woman's health. Hamilton pointed out that Congress purposely set out to demonize the procedure--by referring to it as "infanticide," which is "grossly misleading and inaccurate," she wrote.</P>
<P>While the ruling specifically prohibits the Justice Department from enforcing the ban at any of Planned Parenthood's 900 clinics, the law remains in effect otherwise--pending the outcome of two other court challenges. Whatever the outcome of those cases, Hamilton's ruling likely means that the so-called "partial birth" ban will eventually go before the Supreme Court.</P>
<P>That's why it's more important than ever for abortion rights supporters to build a movement that sends a message to the courts and politicians that we won't go back. Unfortunately, however, much of the mainstream pro-choice movement today seems more focused on getting Democrat John Kerry elected in November.</P>
<P>National Organization for Women President Kim Gandy applauded Hamilton's decision, saying, "The current administration's attempt to outlaw a proven, medically safe procedure shows they will stop at nothing to reverse abortion rights granted by <I>Roe v. Wade</I>." But Gandy seems to have amnesia when it comes to Democrats.</P>
<P>In all, 63 House and 11 Senate Democrats joined Republicans in voting in favor of banning the late-term procedure. Kerry himself ducked the vote entirely. As the <I>Washington Post </I>recently noted, Kerry "rarely talks about the issue on the campaign trail--unless he is forced to."</P>
<P>When he is "forced to," he's proven to be an unreliable ally. Last month, for example, Kerry told reporters that he would consider appointing anti-choice judges to federal courts--as long as it didn't lead to the Supreme Court overturning <I>Roe v. Wade.</I> But that could mean more judges to chip away at abortion rights even as they allow abortion itself to remain technically "legal." </P>
<P>Planned Parenthood President Gloria Feldt excused Kerry, saying, "I think John Kerry understands viscerally reproductive rights as being related to women's human rights globally. But he's got to come up with some better language to talk about it, and I think he's being poorly advised, poorly served by some of his advisers at the moment.''</P>
<P>The problem isn't "bad advice." John Kerry will say (or not say) anything he wants about abortion rights as long as he believes he has the pro-choice vote in his pocket.</P>
<P>With abortion rights hanging by a thread and more court challenges yet to come, we can't trust Kerry to protect the right to choose. Now more than ever, we need to focus on building a movement in the streets that can fight for abortion rights.</P>


