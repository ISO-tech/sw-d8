<p><a name="Voice"></a><font face="Times New Roman, Times, serif" size="4"><b><I>Socialist Worker </I>is my voice</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
"According to NBC, there are allegations that boys were raped by Iraqi guards at Abu Ghraib. British newspapers reported that some children might have been tortured." Those are the horrifying final two sentences at the end of a May 12 article in our local newspaper, the <I>Austin American-Statesman</I>.</P>
<P>It had been building up inside me since the photos of torture were first revealed, but it was when I read these lines that I really felt I might go crazy. That's because the news from today was dominated by the quotes from senators, who just today viewed the full extent of the numerous photos and videos of torture. </P>
<P>What did they have to say? Did they--Republican or Democrat--speak to the horror and outrage that I and hundreds of millions across the world are feeling? No. The senators were worried that releasing the photos and videos to the public might "jeopardize more lives" of Americans in Iraq. </P>
<P>It was only when I picked up my weekly copy of <I>Socialist Worker</I> at an anti-death penalty meeting that I was reminded who does speak for me. The paper's cover--with a photo of the torture and the headline "This is what occupation looks like"--reminded me that amid the loads of corporate media lies there is a newspaper that is my voice. Not just because it reports the truth, but even more because it points a way forward.</P>
<P>That's why I'm taking extra copies of the <I>SW </I>to school to sell to the people I work with. And that's why we're working hard in Austin to bring as many people as possible to the Socialism 2004 conference in Chicago June 17-20. See you there!<BR>
<B>Quent Reese, </B>Austin, Texas</P>

