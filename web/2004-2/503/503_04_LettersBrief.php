<p><a name="Nader"></a><font face="Times New Roman, Times, serif" size="4"><b>Nader has to build a left-wing fight</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Ralph Nader recently was awarded the endorsement of the Reform Party USA, the anti-immigrant, anti-working-class group that ran &uuml;ber-bigot Pat Buchanan as their presidential candidate in 2004. Unfortunately, instead of rejecting the support of a party that scapegoats immigrants, Nader accepted the endorsement, saying, "This endorsement shows that our independent campaign is receiving support from across the political spectrum." </P>
<P>As if being backed by racists and xenophobes were something to be proud of! All of this comes without a peep of protest from Nader or his campaign over the Reform Party's blatant bigotry. </P>
<P>In one of the longest sections of the party's platform, these bigots lay bare their plans on how to "deal" with immigration: a freeze on all immigration, use of the military to secure borders, a national campaign of assimilation and forced learning of English, a Constitutional amendment to take away citizenship of children born in the U.S. to non-citizens, and more.</P>
<P>Those on the left need to argue, both within his campaign and to potential supporters, that Nader must not pander to the Reform Party constituency in any way. In addition, given the record of the Reform Party, it is imperative that Nader make clear his opposition to their racist immigrant-bashing and that he include immigrant rights as a prominent issue in his campaign.</P>
<P>Nader has an excellent opportunity to tap into and solidify growing antiwar sentiment, plus the frustration felt by millions of Americans over the lack of any real alternative to imperialism abroad and giant corporate tax breaks at home--but not if he kowtows to the right in an effort to appeal to a "wide spectrum" of potential supporters. A campaign that consciously tries to break the hold of the Democratic Party on the left is worth supporting. The question is: will Nader seize the opportunity or squander it?<BR>
<B>Michael Smith, </B>Berkeley, Calif.</P>

