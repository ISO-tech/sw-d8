<P><font face="Times New Roman, Times, serif" size="4"><b>We told Romney: "Separate is never equal"</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
A few months ago, students of Suffolk University, a school that prides itself on its "commitment to diversity," learned that the notorious Massachusetts Gov. Mitt Romney--who is committed to oppression--was to speak at Suffolk's commencement ceremony.</P>
<P>One graduate explained that the university president eluded virtually all of the democratic rights of the students in the decision. We would not let our voices go unheard. Students for Peace and Justice and the Rainbow Alliance organized an alternative ceremony the day before, celebrating, instead of undermining, diversity--and at the commencement itself passed out armbands of the Human Rights Campaign's "equals" sign.</P>
<P>A few of us needed to take further action against Romney, who has consistently attacked workers, wants to reinstate legal lynching in Massachusetts and was quoted in the <I>Boston Globe</I> as describing homosexuality as "perverse" and "reprehensible." We were allowed into the commencement with International Socialist Organization placards saying "Separate is not equal--Gay marriage is a civil right," but the police then forced us to leave the signs outside without any explanation.  </P>
<P>But when the "honorable" bigot took the stand, three of us did whatever we could to have our boos heard, and joined a few grads in standing and turning our backs on him. Despite cooperation with the ushers, the boo-ers were manhandled and removed by the police (who wouldn't identify themselves).  </P>
<P>When we exited, we found our placards had been stolen, and we were threatened with arrest for merely requesting their return. We thought we had failed to make an ample statement, but later found that the grads who protested were joined by others and later were overwhelmed with statements of solidarity.  </P>
<P>Although we were repressed, the action was polarizing and politicizing to those present, and fostered the confidence of our side to fight--especially since it was covered by the <I>Globe</I>, the <I>Herald</I>, and even by NBC news. It shows that just a few people can be effective in meeting the need to challenge the right.<BR>
<B>Tom Arabia and Derek Medley, </B>Boston</P>

