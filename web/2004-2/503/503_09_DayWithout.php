<P><font face="Times New Roman, Times, serif" size="5"><b>Can California live a <I>Day Without a Mexican?</I></b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Justin Akers</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 11, 2004 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2"><B><I>A Day Without a Mexican,</B></I> directed by Sergio Arau, written by Yareli Arizmendi and Sergio Guerrero, starring Arizmendi, Caroline Aaron and John Getz.</font></P>

<P><font face="Times New Roman, Times, serif" size="3">"THE LATINO Riddle" reads the masthead of the CNN-like news program. "Where have all of the Latinos gone?" <I>A Day Without a Mexican</I> explores the hypothetical consequences of the disappearance of all Latinos in California, leaving a desperate and desolate population without more than a third of its people.</P>
<P>A looming El Ni&ntilde;o storm surrounds the California border, seething as acts of racism and discrimination toward Latinos are shown to be commonplace. A Border Patrol agent ruthlessly clubs a fleeing migrant, a wealthy senator promotes anti-immigrant legislation, and a sleazy producer humiliates a Mexican-American news reporter who isn't acting "Mexican enough" to give his channel the veneer of diversity.</P>
<P>Within hours, every Latino in the state disappears. The movie chronicles the degeneration of California as it becomes clear just how integral Latinos are to daily life. Fruit rots on the vine, schools are forced to close, restaurants are vacant, and the households of the wealthy--without servants--spin into turmoil. Musical groups, news reporters and senators vanish. With each vanishing, the script is punctuated with flash points of hard facts about Latinos, dispelling the myths, lies and half-truths that perpetuate ignorance.</P>
<P>While the Latino working class is undoubtedly central to the functioning of capitalism in California, the movie is careful to avoid stereotyping Latinos as the indispensable "economic underclass." Interracial families are broken up, friendships are lost, and social cohesion deteriorates as people realize that racial diversity and integration benefits us in innumerable ways.</P>
<P>The seriousness of the storyline is periodically interrupted by moments of refreshing humor and wit. In one scene, a ranch owner petitions the governor to put the now unemployed Border Patrol to work in the fields. In another scene, a "UFO expert" postulates that Latinos are actually from space--why else would the U.S. government use the dehumanizing term "alien" to describe immigrants?</P>
<I><P>A Day Without a Mexican</I> is brilliant in its aims. While there are a few potholes in the story, what shines through is an intelligent and entertaining, pro-immigrant rebuttal to the right-wing garbage that dominates immigration politics in the U.S.</P>


