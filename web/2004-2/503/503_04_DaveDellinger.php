<P><font face="Times New Roman, Times, serif" size="5"><b>Obituary: David Dellinger</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 11, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</I> <BR>
</B>Many times as a young activist, you are confronted with the cynical crust of an older movement saying, "I used to think exactly like you"--in an attempt to let us know that, one day, we'll grow up, too. Dave Dellinger, who passed away last month after a lifetime of activism, was always the remedy to that kind of poisonous disparagement.</P>
<P>Radicalized by the poverty of the Great Depression, Dave began challenging the system that caused it by becoming a conscientious objector to the Second World War. During the war, Dave was one of the few voices who understood that the United States was not fighting for the liberation of humanity, but more precisely for the domination of it. </P>
<P>He earned his fame, such as it was, as the "old guy" of the Chicago Eight, who were tried after the 1968 Democratic National Convention for, in short, conspiring to start a riot. After the collapse of the movements of the 1960s and '70s, Dave continued leading forces on the left towards a better world. </P>
<P>Marching in protests with him was a radical history lesson. Hearing all the places he had once demonstrated at and all the causes he had worked for contributed greatly to many activists' own radicalization.</P>
<P>To be clear, we had our disagreements. His life was dedicated to nonviolent resistance, and he opposed tactics that strayed from that ideal. But he never equated the violence of the oppressed with the violence of the oppressor. </P>
<P>In a 1993 interview, he stated, "I prefer to call myself a nonviolent activist for justice--or justice and peace--rather than a pacifist. Because too often pacifists treat the violence of war and weaponry as more deadly than the violence of our economic institutions. But more people die every week because of the poverty caused by our economic institutions than the total number of GIs killed in the entire Vietnam War."</P>
<B><P>Jim Ramey, </B>Burlington, Vt.</P>


