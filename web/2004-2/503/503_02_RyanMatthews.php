<P><font face="Times New Roman, Times, serif" size="4">Louisiana prosecutors won't let go of an innocent man</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why is Ryan Matthews still in jail?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 11, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">LOUISIANA'S DEATH machine won't let go of an innocent man. Former death row prisoner Ryan Matthews remains behind bars today after a June 3 court hearing at which prosecutors once again demanded a delay.</P>
<P>Ryan was convicted and sent to death row for the 1997 murder and robbery of a grocery store owner. A year ago, Ryan's lawyers announced that DNA testing of a ski mask used in the crime proved that Ryan couldn't have been the killer--and implicated another man currently in jail for murder.</P>
<P>After dragging their feet for the next year, prosecutors agreed in April that Ryan should get a new trial. But at a court hearing where Ryan's family and friends hoped that the judge would set bail, the state announced that it had miraculously discovered new evidence on the ski mask--a previously undetected eyelash and skin cells, it claimed--that it wants tested.</P>
<P>This will be the <I>fifth </I>time the mask has been subjected to DNA testing. "There's something terribly wrong with the justice system," Pauline Matthews, Ryan's mother, said in an interview with the <I>New Abolitionist</I>. "Four DNA tests came back, and now they're saying they have to do another."</P>
<P>Ryan--who was forced to appear in handcuffs and shackles--will get another hearing at the end of June. But this is justice too long delayed.</P>
<P>From the beginning, Ryan has been a victim of fanatical prosecutors and a racist system. He was arrested for the murder just two months after his 17th birthday. The trial that landed him on death row lasted all of three days, and the jury that convicted and sentenced him was almost all white.</P>
<P>But this is par for the course in Jefferson Parish--the home of Klansmen David Duke, where the prosecutors who railroaded Ryan are known for wearing neckties decorated with nooses. Now, the system that sent a mentally retarded Black youth to death row for a crime he didn't commit is determined to delay justice as long as it can--anything to not admit its crime.</P>
<P>"With the delay today, we weren't surprised," Pauline Matthews said. "But for them to deny Ryan justice for so long! If it were their child, if Ryan was white, if we had money, he would have been freed by now."</P>


