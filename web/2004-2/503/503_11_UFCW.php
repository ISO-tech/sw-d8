<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Why the UFCW won't fight</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Darrin Hoop, UFCW Local 1105</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 11, 2004 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE UNITED Food and Commercial Workers (UFCW) union has once again extended the contract covering 25,000 grocery workers in the Puget Sound region past its initial deadline of May 2. Kroger Co., Safeway Inc., and Albertsons Inc., have forced contracts onto grocery workers from Southern California to Washington, D.C., that contain hundreds of millions of dollars in concessions in health care benefits, pensions and wages. </P>
<P>Here it is no different. Still, the UFCW has yet to take a strike vote and continues to try to negotiate away the concessions.</P>
<P>Why is the UFCW leadership hesitant to lead a fight? A look at the UFCW's history will give some insight.</P>
<P>From its inception, the UFCW's growth has come predominantly from mergers. If it weren't for mergers, the union would have lost more than 800,000 members since 1979, according to reports filed with the U.S. Department of Labor.</P>
<P>There are no direct elections of International officers. As a result, they've been able to set up a patronage system of high salaries and extravagant "expense" accounts. For example, Douglas Dority--the UFCW's international president during the Southern California strike--received more than $336,000 in salary and "expenses" in 1998 alone. There are at least 175 UFCW officials around the U.S. who are paid more than $100,000 a year.</P>
<P>Corruption is rampant in the UFCW. Between 1992 and 1998, there were three different cases involving International officers embezzling a total of $4 million in union members' dues.</P>
<P>When the rank and file has engaged in strikes and pushed beyond the conservatism of the International, the International has stabbed the workers in the back. UFCW Local P-9 struck Hormel in Austin, Minn., in 1984, but after seven months on strike, the International cut off strike benefits, eventually put the local in trusteeship and forced a horrible contract onto the workers.</P>
<P>In the current issue of the UFCW's national newsletter "Working America," the lead article says, "The real way to get health care under control isn't on the picket line. Working people need a Congress and a President who will enact health care reform." The union plans a massive mobilization to get John Kerry elected--despite the record of betrayals by Democrats once they get elected. In 1992, Bill Clinton promised to deliver health care reform if elected, but despite Democratic control of the White House, Senate and the House of Representatives, he never delivered.</P>
<P>The UFCW leadership has pursued a partnership with the corporate and political elite in the U.S. while it has enriched itself at the expense of the members. Now, with this latest round of contract negotiations, the Big Three grocery stores are exploiting the union's weaknesses. </P>
<P>The result is a membership that hasn't been educated and mobilized enough to take the companies on. Most workers are angry, but want all this to end. To push the UFCW leaders to fight, it will take first an understanding of why the union leaders aren't fighting. Then it will be up to the rank and file to organize and educate--from one coworker to the next. </P>
<P>All of the grocery contracts that are currently negotiated separately around the U.S. need to be linked into a national master grocery contract. Strike preparation must begin many months in advance of a contract deadline. A poorly organized strike will always lose.</P>
<P>Locals need to take steps to avoid the weaknesses of the Southern California strike--for example, insuring that pickets go up at every single grocery store, building mass pickets so scabs will not be able to cross picket lines and organizing solidarity with other unions, especially Teamsters, so they won't cross picket lines. Like in the 1930s when union officials refused to lead a fight, it will be up to rank-and-file activists to lead the rebuilding of the U.S. labor movement.</P>


