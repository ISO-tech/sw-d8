<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Corporate America's one-sided class war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 11, 2004 | Page 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE PRESIDENT who reclassified ketchup as a vegetable in poor children's school lunches may have finally stopped breathing, but the Reagan Revolution is already well into its second incarnation--far outstripping the excesses of the first. According to <I>Business Week's</I> 54th Annual Executive Compensation Survey, the ratio of workers' pay to CEO compensation reached 301-to-1 in 2003, when workers' weekly earnings averaged $517 and the average CEO took home $155,769 a week. </P>
<P>While the financial wealth of the richest 1 percent of the population grew 109 percent between 1983 and 2001, the bottom two-fifths' fell 46 percent. A study by the World Bank last year found the gap between the incomes of the wealthiest and poorest 30 percent of families was greater in the U.S. than in the 11 other advanced industrialized nations it surveyed.  </P>
<P>According to Edward N. Wolff, an expert on wealth concentration, in 1976, the richest 10 percent of U.S. families held 50 percent of the nation's wealth; by 1995 they held 70 percent of all wealth, and the top 20 percent of families owned 83 percent of wealth--with the remaining 80 percent of families holding only 17 percent.</P>
<P>The gap between rich and poor is now officially bigger than at any time since Herbert Hoover was president. As a consequence, living standards are in a downward spiral for the working class in the richest nation in the world.</P>
<P>Life expectancy has actually begun to drop in the U.S. People live longer on average in Costa Rica, a country with a per capita gross national product one-tenth of the United States. Infant mortality in the U.S. is higher than in Cuba. And more than one in four workers today have no access to employer-based health insurance.</P>
<P>As James Lardner of Inequality.org argued, "In the developed world, at least, population health appears to depend less on national or per capita income than on the way income is apportioned. Thus," he continued, "Greece, where the average citizen earns about one-half as much as the average American, outdoes the United States in most indices of good health, including longevity."</P>
<P>Despite economists' rosy forecast of low unemployment in the months to come, there are indications that the government's official unemployment statistics grossly understate the problem. For example, a recent local survey of Lawrence County, Ohio's unemployment rate shows unemployment is three times worse than is being reported by the U.S. Department of Labor.</P>
<P>According to the government, Lawrence County's unemployment rate was just 5.7 percent in April. But when county officials did their own survey last month, they estimated the real number was 17.9 percent, according to the local <I>Herald-Dispatch. </I>In the Lawrence County survey, more than 20 percent of families were found to be living below the official poverty level.</P>
<P>New jobs have been added to the economy over the last few months, finally reversing three years of decline, but the new jobs on offer pay less than the ones that have been lost. According to the Economic Policy Institute (EPI), "averaging over the past three months, and comparing this year to last year, industries expanding as a share of total employment pay on average 13 percent below industries contracting." And nearly one in 10 new jobs added since February is a temp job.</P>
<P>There are plenty of young workers to fill these new low-paying jobs, with a four-year university education out of reach for a growing number of working-class families with children. "This may be the first generation in American history that won't be better educated" than the one before, according to National Center for Public Policy and Higher Education president Patrick Callan.</P>
<P>This is also the first generation of young workers in U.S. history that faces a lower standard of living than their parents. "If class warfare is being waged in America, my class is clearly winning," CEO Warren Buffett gloated in his annual letter to the shareholders of Berkshire Hathaway.</P>
<P>But for three consecutive generations, "class warfare" in the U.S. has been completely one-sided. It is high time the union movement started to fight back.</P>


