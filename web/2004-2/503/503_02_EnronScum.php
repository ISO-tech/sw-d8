<P><font face="Times New Roman, Times, serif" size="5"><b>Enron's corporate criminals</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 11, 2004 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">FROM THEIR own mouths, we have the evidence--that the crooks at Enron were human pond scum. CBS News obtained old audiotapes of employees at the bankrupt energy giant boasting about how they were "fucking over" California by manufacturing an energy crisis that, all told, netted an estimated $20 billion in inflated electricity prices.</P>
<P>The tapes are of Enron's West Coast trading desk during 2000, after local utility companies began to complain about Enron's sleazy manipulations. "They're fucking taking all the money back from you guys?" one trader asks. "All the money you guys stole from those poor grandmothers in California?" </P>
<P>"Yeah, Grandma Millie, man," comes the reply. "Yeah," says a trader, "now she wants her fucking money back for all the power you've charged right up, jammed right up her ass for fucking $250 a megawatt hour." </P>
<P>But the traders weren't too worried about protests from "Grandma Millie" or anyone else--because they expected to have their man in the White House after the November election. "When this election comes," an Enronite declares, "Bush will fucking whack this shit, man. He won't play this price-cap bullshit." </P>
<P>And what did Bush have to say about capping the price of electricity in May 2001? "We will not take any action that makes California's problems worse, and that's why I oppose price caps."</P>


