<P><font face="Times New Roman, Times, serif" size="4">Maryland wants to restart its execution machine</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Fighting for Steven Oken</b></font> </P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 11, 2004 | Page 4</font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</B></I> <BR>
Maryland Gov. Bob Ehrlich is devoting his time and energy in the next two weeks to killing Steven Oken. He wants Oken to become the first person executed in Maryland since 1998. But abolitionists are saying loud and proud that what Bobby wants, Bobby may not necessarily get. </P>
<P>Our battle to save Steven Oken will not be easy. Pro-death judges, backed by Governor (and Dubya-wannabe) Ehrlich, call Oken "the poster child" for the death penalty because he is white and from a "middle class" background. Oken admitted his guilt in the 1991 rape and killing of Dawn Garvin, a white woman, in Baltimore County.</P>
<P>This, they think, will allow them to get around those pesky studies that show Maryland has the most racist death row in the U.S. This, they think, will mute public outrage over a Maryland system that has been shown recently to botch lethal injections. This, they think, will close our eyes to the fact that 85 percent of murder victims in Maryland are Black, but death row is <I>exclusively</I> 100 percent populated by people accused of killing whites. </P>
<P>They think this, but they are wrong. In a church basement last week, more than 100 people gathered to hear a dazzling lineup of speakers stand up against death row. Included were the NBA Washington Wizards' center Etan Thomas, former death row inmates Darby Tillis and Shujaa Graham, state Delegate Salima Marriott, members of Steven Oken's legal team, and Campaign to End the Death Penalty and International Socialist Organization member Michael Stark, among others. </P>
<P>The event was described by the <I>Washington Post</I> as having the energy and passion of a "revival meeting." Let the message be clear: neither Steven Oken nor our movement will go gently into the night. Bob Ehrlich and his apparatus of legal lynchings have a fight on their hands. <BR>
<B>Dave Zirin, </B>Prince George's County, Md.</P>


