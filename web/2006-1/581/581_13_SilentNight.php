<font face="Times New Roman, Times, serif" size="4">World War One and the 1914 Christmas truce</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A silent night</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Donny Schraffenberger</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 24, 2006 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Joyeux No&euml;l (Merry Christmas), </b></i>directed by Christian Carion, starring Diane Kruger and Benno F&uuml;rmann. French with subtitles.</font><p>

<font face="Times New Roman, Times, serif" size="3"><i>JOYEUX NO&Euml;L</i> tells the incredible story of the 1914 Christmas truce on the Western Front during the First World War.<p>
In August 1914, the major European powers of Britain, France, Russia, Austria-Hungary and Germany began what would become one of the deadliest wars in history. Within a few months, hundreds of thousands of soldiers had been killed. <p>
On the Western Front, the German offensive was stopped outside of Paris in September, and by December, the infamous trenches of World War One extended from Switzerland to the English Channel.<p>
<i>Joyeux No&euml;l </i>("Merry Christmas" in French) was nominated this year for an Oscar for best foreign language film. The beginning of the film portrays school kids from the warring countries reciting jingoistic passages, implying that the support of war must be taught and is not inherent in human nature.<p>
Also, we get a quick overview of some of the main characters' civilian lives at the outbreak of war, for most of the soldiers were reservists and later on drafted troops. The movie has a hellish scene of a Scottish and French attack on a German trench, with scores of dead and wounded men littered across no man's land.<p>
By December, most soldiers on all sides have become weary of the conflict. A French soldier longs to have coffee with his mother, who lives a few miles down the road in German-occupied France.<p>
A German officer recalls his honeymoon in Paris with his French wife. Soldiers miss their families, and their civilian lives.<p>
<i>Joyeux No&euml;l </i>contrasts the soldiers' humanity against the Scottish, French and German generals' cold-hearted militarism. The movie doesn't take sides on which country's soldiers were the "good guys" and the "bad guys"--rather the enemies are those higher-ups that make the soldiers fight in the first place.<p>
During that first Christmas of the war, a truce actually happened among the warring sides on sections of the Western Front. Soldiers stopped killing each other, exchanged food and drink, and played cards and soccer. They sang familiar songs and recalled their stays in each other's countries.<p>
In the movie, this fraternization helps break down their dehumanization of each other, so much so that the soldiers become concerned for one another's safety. When the top brass on all the sides find out of this affair, they break up or move the soldiers to different locations, for fear that this comradely attitude could spread throughout the front.<p>
Although <i>Joyeux No&euml;l</i> only depicts the Christmas truce of 1914, eventually the rebellions that the generals fear did take place. In 1917, French soldiers had a widespread but sadly unsuccessful mass mutiny, and in Russia the soldiers revolted and refused to fight any longer in the imperialist war. And in 1918, German sailors rebelled and participated in the German revolution that led to the end of the war.<p>
These well-grounded fears of fraternization, and the need to squash it, are illustrated in the movie when a high-ranking church official sanctimoniously orders Scottish troops to kill Germans because it is God's will. Likewise, a French and German general are upset and punish "their" soldiers for taking part in the unofficial truce.<p>
Overall, <i>Joyeux No&euml;l</i> pulls at your heartstrings. At times, the movie is somewhat over the top in its sentimentality but makes up for this shortfall with its antiwar message. No, it isn't a classic like Lewis Milestone's 1930 film <i>All Quiet on the Western Front,</i> Jean Renoir's 1937 <i>Grand Illusion</i> or Stanley Kubrick's 1957 <i>Paths of Glory</i>--films all dealing with the horror of the First World War.<p>
Nevertheless, in this time of war when the U.S. government and the media portray Arabs and Muslims as barbaric terrorists, it's good to have a movie that shows the comradeship of the common soldiers contrasted against their barbaric Christian ruling-class generals.<p>

