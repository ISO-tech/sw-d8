<font face="Times New Roman, Times, serif" size="4">Outcry over tribute to slain Panther Fred Hampton</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Smeared by Chicago cops</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 24, 2006 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO'S FRATERNAL Order of Police (FOP) has whipped itself into a frenzy over a recent city council proposal to rename a single block of a single street after the late Black Panther leader Fred Hampton.<p>
The FOP and its political allies--turning reality upside down--have railed against the supposed anti-police violence of the Black Panthers. But it was Hampton who was murdered by the Chicago police in the early hours of December 9, 1969, in a hail of 90 bullets, along with fellow Panther Mark Clark, as he lay in bed beside his pregnant girlfriend. During the raid, the victims managed to get off just one shot in self-defense.<p>
It isn't surprising that the FOP blasted the proposal to honor Fred Hampton. A "Fred Hampton Day" in 1990 elicited a similar police freak-out. The street would be an unwelcome reminder of what Hampton and the Black Panther Party represented.<p>
The Panthers argued that Blacks had a right to defend themselves--by whatever means necessary--against constant racist attacks and police brutality. <p>
In the late 1960s, their popularity skyrocketed. For that, they were brutally repressed by the police and FBI. FBI Director, J. Edgar Hoover declared the Panthers the "greatest threat to the internal security of the country." The raid in Chicago was just one of dozens of attacks around the country.<p>
Unable to muster a defense of the Black Panthers' politics, the alderman who proposed the honorary street, Madeline Haithcock claimed she was only "going on the good things they did," like the Panthers' free breakfast programs. Then she folded completely, claiming she didn't "want to cause dissension among our police officers...with all of the negative things the Black Panthers did."<p>
While Chicago politicians might occasionally give lip service to honoring a Fred Hampton or Malcolm X, they are part of a Democratic Party machine that to this day persecutes and brutalizes Blacks and Latinos. Mayor Richard M. Daley is the son of Richard J. Daley, who reigned during Hampton's murder and green-lighted "shoot-to-kill" orders during the riots that followed Martin Luther King's assassination. <p>
Richard M. Daley cut his political teeth as state's attorney in the 1980s--at the height of Chicago Police Commander Jon Burge's notorious torture ring. Amnesty International, and even the police department, concluded that Burge and his underlings tortured more than 100 Black men through electroshock, beatings, suffocation and Russian roulette. Daley made his name--what he didn't get from his father--prosecuting victims of torture.<p>
The racism and torture of the Chicago police were exposed, in no small part because torture victims on death row, the Death Row Ten, began to organize and speak out. Since 2000, there has been a moratorium on the death penalty in Illinois--and in 2003, Illinois' death row was cleared out by former Gov. George Ryan. <p>
Now, the current state's attorney, Dick Devine--who once worked for a private firm that represented Jon Burge--and the rest of the machine are trying to rehabilitate the image of the criminal justice system and lift the moratorium.<p>
For them, there couldn't be a worse time to honor Fred Hampton--or the fighting legacy of the Black Panthers. One street is too much for them, and not nearly enough for us.<br>
<b>Julien Ball and Adam Turl,</b> Chicago<p>

