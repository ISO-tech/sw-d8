<font face="Times New Roman, Times, serif" size="5"><b>What's behind HERE-UAW takeover of union at NYU?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 24, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>SARAH WOLF, a striking member of GSOC at New York University, reports on recent developments in her union.</b><p>

NEW YORK--Members of the Graduate Student Organizing Committee (GSOC) learned at a meeting March 15 that our local, UAW Local 2110, will no longer be running our strike at New York University (NYU).<p>
In a surprise move that had clearly been in the making for some time, all Local 2110 staff have been pulled out of strike organizing duties (although they have not been fired), and all operations will be run by a joint team of regional and international UAW representatives and paid UNITE HERE organizers from GESO, the Yale graduate student union (which has never won a contract). <p>
Our local president, Maida Rosenstein, no longer has authority over strike strategy or operations--a move made without the say of GSOC members. Meanwhile, 12 rank-and-file GSOC organizers will be hired as paid staff by the new strike leadership. The March 15 meeting was attended by both UNITE HERE President John Wilhelm and UAW Secretary-Treasurer Elizabeth Bunn.<p>
While this is not the first joint organizing venture embarked on by the two unions--they jointly represent casino workers in Detroit--UNITE HERE is now outside the AFL-CIO and part of the Change to Win federation.<p>
The UAW and UNITE HERE are both involved in high-stakes fights at Delphi and at hotels nationwide respectively--so why have they suddenly devoted high-level leadership and resources to our small strike in an industry into which UNITE HERE claims not to want to expand? <p>
One possibility is that, if nominally "won," our strike would vindicate the Change to Win strategy--consolidation of bigger unions and more funds devoted to organizing (hence the hiring of more paid staff).<p>
Yet the "new" strategy proposed by those taking over the strike is the one that put us into our currently weak position. This strategy involves symbolic, briefly disruptive actions on and off the NYU campus to demonstrate union members' support for the union, while hoping politicians convince NYU to negotiate.<p>
The new leaders plan an on-campus rally March 24 and a campaign to get members who have left the strike to sign a letter stating that they still want a union. The letter is clearly intended to impress the Democratic Party politicians and not our boss, who loves the idea of crushing a union that has majority support.<p>
What's new is that the HERE-UAW leadership is clearly confident that we can "win" this strike fairly quickly. Some members suspect that this means accepting the fake "contract" offered by NYU in August--an agreement that eliminates any external grievance procedure and institutes an open shop, among other unacceptable givebacks.<p>
Nevertheless, the best thing for union militants to be doing right now is indeed organizing our membership with the aim of convincing members to rejoin the strike.<p>
There's no reason for NYU to negotiate with our union--in the absence of political pressure or a massive return to the strike by graduate instructors who have gone back to work. But if we can use the influx of resources to expand the strike, we might have the ability to win a real contract.<p>

