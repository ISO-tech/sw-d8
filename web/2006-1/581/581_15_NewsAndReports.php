<p><a name="Hillary"></a><font face="Times New Roman, Times, serif" size="4"><b>Protesting Hillary Clinton</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jessica Baliant</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ALBANY, N.Y.--A coalition of student groups protested the appearance of Sen. Hillary Clinton (D-N.Y.) at the State University of Albany as part of a March 10 telecommunications panel.<p>
The demonstration was in response to her continued support for the occupation of Iraq as well as her unwillingness to offer up an explanation as to why she will not call for the immediate withdrawal of troops from the Middle East.<p>
The conference was comprised of a strictly suit-and-tie crowd showcasing 20 upstate telecommunication companies while many students, many of whom were registered for the event, were prevented from entering the campus center ballroom by police and Secret Service agents. <p>
Eventually, several protesters were allowed in--but the head of campus police made it clear that any type of outburst would be met with arrest. In response, the students placed duct tape on their mouths with the words "free speech" written on them.<p>

