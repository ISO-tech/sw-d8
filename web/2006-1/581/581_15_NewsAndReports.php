<p><a name="Racism"></a><font face="Times New Roman, Times, serif" size="4"><b>Fighting anti-Arab racism</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Thirty people packed a small room for a panel about the racist Danish cartoons. The event was organized by the International Socialist Organization (ISO) and the Muslim Students Association (MSA). <p>
"The war on terror is a war on immigrants," said Fahd Ahmed, one of the panelists and a member of the South Asian immigrants rights group Desis Rising Up and Moving.<p>
The "war on terror" has ruined people's lives, led to deportations of whole families and created a climate of fear that pervades these communities, he continued. <p>
MSA's Delia Dean focused on the media's manipulation of images and compared the racist stereotypes of Arabs and Muslims to the labeling of Hurricane Katrina's mostly poor and Black victims as "looters."<p>
ISO member Pham Binh explained that "all imperialist wars require racism," which in the past led to Japanese internment and today has led to the widely circulated claim that Iraqis are "unfit" for self-rule in order to justify the ongoing U.S. occupation.<p>
Contributions from the audience led to friendly but impassioned debate about Israel's role in the Middle East, freedom of speech and racism in the "war on terror." The event was a step forward for the left and groups like the MSA at Hunter College and hopefully will serve as the basis for future collaboration around antiwar and antiracist activism.<p>
