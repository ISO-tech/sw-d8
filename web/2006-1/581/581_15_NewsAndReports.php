<font face="Times New Roman, Times, serif" size="4"><b>Say no to war</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Robin Gee</b></font><p>

<font face="Times New Roman, Times, serif" size="3">MADISON, Wis.--Wisconsin is among the growing number of states where efforts are underway to bring the issue of the war directly to voters. Supporters say that these initiatives serve to send a message that a majority of U.S. citizens strongly oppose the war and that they offer people the opportunity to speak out directly.<p>
Thirty-two Wisconsin municipalities will have the opportunity during the April 4 statewide elections to vote on referendums calling for the immediate withdrawal of U.S. troops from Iraq. <p>
Many cities and towns across the U.S. have passed antiwar resolutions. In Vermont, a statewide effort to pass antiwar resolutions through that state's town meeting structure garnered widespread support. <p>
Wisconsin is the first state, however, to put an antiwar referendum up for vote on local election ballots. Hundreds have helped gather signatures to get the referendums on the ballot and many continue to help with yard-sign distribution, literature drops and phone calls in an effort to get out the vote April 4. <p>
<i>For more information or to get involved, call 608-250-9240</i>.<p>
