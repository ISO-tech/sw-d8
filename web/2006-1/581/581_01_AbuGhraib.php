<font face="Times New Roman, Times, serif" size="5"><b>U.S. can't cover up its record of torture</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 24, 2006 | Pages 1 and 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE PENTAGON is getting ready to pull out of Abu Ghraib prison--the notorious site of the torture of Iraqi prisoners by U.S. military forces.<p>
Earlier this month, following a Senate hearing, Marine Gen. Peter Pace, chair of the Joint Chiefs of Staff, announced that the U.S. would turn over control of Abu Ghraib to the Iraqi government. Abu Ghraib became synonymous with the abuse of detainees after pictures were made public in 2004 of U.S. soldiers posing with tortured Iraqi prisoners.<p>
U.S. officials claim that the prison is a frequent target for insurgent attacks and would be better off managed by Iraqis. But the more likely explanation is that the U.S. is eager to distance itself from continued allegations of brutality toward Iraqi prisoners--and sees turning the prison over to the Iraqi government as a way to do so.<p>
While removing American control over Abu Ghraib may get rid of a symbol of U.S. abuses, it won't halt them.<p>
According to revelations in the <i>New York Times,</i> for example, an elite Special Operations force known as Task Force 6-26 tortured Iraqi prisoners--both before and after the Abu Ghraib scandal broke--at a secret detention center known as Camp Nama, near the Baghdad airport.<p>
Using what the <i>Times </i>described as "one of the former Iraqi government's torture chambers," members of Task Force 6-26 beat prisoners with rifle butts, yelled and spit in their faces, and used detainees for target practice in a game of "jailer paintball." Other prisoners were seen with burn marks covering their bodies.<p>
Acts like this make it clear that the U.S. occupiers can't be trusted to stop the abuse. And Pace himself admits that detainees at Abu Ghraib may simply be moved to new prisons under U.S. control--leaving open the possibility that similar acts will be carried out elsewhere.<p>
Those new "facilities" would include a new detention center at Camp Cropper--the prison on the U.S. military base at Baghdad's airport, where "high-value detainees" are now held, and allegations of abuse are frequent. In all, the U.S. today continues to hold more than 14,000 prisoners at four facilities in Iraq.<p>
According to "Beyond Abu Ghraib: Detention and Torture in Iraq," a report released earlier this month by Amnesty International, the U.S. and its allies continue to commit widespread abuses of detainees in Iraq, including torture and indefinite detention of thousands of prisoners.<p>
"Many cases of torture and ill treatment of detainees held in facilities controlled by the Iraqi authorities have been reported since the handover of power in June 2004," the report said. "Among other methods, victims have been subjected to electric shocks or have been beaten with plastic cable. The picture that is emerging is one in which the Iraqi authorities are systematically violating the rights of detainees in breach of guarantees contained both in Iraqi legislation and in international law and standards."<p>
And, says the report, U.S. military forces were often complicit in the abuse. "Former detainees who were subjected to torture or ill-treatment or who witnessed the infliction of such abuses on fellow detainees while they were being held in the custody of the Iraqi authorities, have told Amnesty International that such incidents occurred with the knowledge or even in the presence of U.S. troops."<p>

