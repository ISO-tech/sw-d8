<p><a name="AK"></a><font face="Times New Roman, Times, serif" size="4"><b>Locked out by AK Steel</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I JUST finished reading your article about the lockout of the workforce at AK Steel ("AK Steel locks out union," March 10). I liked the article with one exception. In one of the latter paragraphs you called Dan Neal a striker. This is not true. He is a retired employee of AK Steel.<p>
The Armco Employees Independent Federation (AEIF), in fact, did take an authorization vote for a strike. That, as you said in the article, passed with 98 percent of those voting giving the power to the executive board of the AEIF to call for a strike. A side note to that vote is that approximately 90 percent of the membership voted. <p>
The executive board of the AEIF has <i>not</i> called for a strike. We, the workforce at AK Steel, have been <i>locked out</i> by the company.<br>
<b>Robert E. Mattocks</b>, Member, AEIF, From the Internet<p>
