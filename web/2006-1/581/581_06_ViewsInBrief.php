<font face="Times New Roman, Times, serif" size="4"><b>California's flood risk</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WATER, WHERE life began, threatens to end the lives of many in California's Central Valley. As a result, California Gov. Arnold Schwarzenegger proclaimed in a recent letter to President George W. Bush "a state of emergency for the California levee system due to the imminent threat of catastrophic levee failure.<p>
"Increasingly severe weather systems each season have accelerated the deterioration of the state's levee system to the point where they are now in danger of failing during the next major rainfall or earthquake. This worsening situation creates conditions of extreme peril to the public and property protected by the levees, to the environment, and to the very foundation of California's economy."<p>
Schwarzenegger requested $3 billion in federal funding to reduce this flood risk. It was denied. Surprised? I'm not.<p>
Consider that $3 billion represents approximately 19 days of federal spending for the Iraq war, or roughly $6.7 million per hour. Crucially, that $3 billion profits U.S. corporations involved in Iraq. For the federal government, these war firms are primary. That primacy builds value for the companies' shareholders. <p>
This deal, sealed by history, time and money, eclipses federal funding to protect people and property in the Central Valley threatened by catastrophic floods due to leaky levees, weak creeks and shaky storm drains. The fateful situation there is secondary, federally speaking. What is the bottom line? The Central Valley has lost in competition with military contractors for federal dollars. <p>
One outcome is that Gov. Schwarzenegger will add the denied federal funds to his multibillion-dollar bond issuance for the state's residents to vote on. This is sweet music to the ears of bond firms, whenever the exact date of that ballot measure this year. <p>
In the meantime, the Central Valley remains at risk from flooding and flood damage in winter and spring. To wit, some of its levees are a century old! Electronic and print media in the Sacramento region (population of 1,379,103) have been on the flood-threat story in the governor's backyard. <p>
I digest such news and views perhaps as some folks did in New Orleans before the racialized calamity of Katrina and FEMA. Press coverage on the flood risk to the Big Easy prior to last summer's disaster casts an eerie shadow of similarity to current Sacramento reporting on the Central Valley's threat of being flooded. Gov. Schwarzenegger failing to get $3 billion in federal funds to lessen that risk extends the shadow.<br>
<b>Seth Sandronsky,</b> Co-editor, <i>Because People Matter</i>, Sacramento, Calif.<p>
