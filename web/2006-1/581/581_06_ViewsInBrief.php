<p><a name="Dubai"></a><font face="Times New Roman, Times, serif" size="4"><b>Dubai Ports corruption</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THE RACIST reaction to the White House plan to grant Dubai management of six U.S. ports is contemptible. We should condemn it categorically. That does not, however, mean we should champion this cozy $6.5 billion sweetheart deal between billionaire Washington insiders and an oppressive monarchy that owns the global corporation known as Dubai Ports World (DPW).  <p>
A little background: Treasury Secretary John Snow, whose office had to approve the port takeover, is the former CEO of a company acquired by DPW shortly after he joined Bush's cabinet. It looks like a quid pro quo among business cronies, yet Snow claims he knew nothing of the deal until he read the papers. <p>
Democratic Party leaders are no better. Madeleine Albright, Clinton's Secretary of State, has been chief lobbyist for DPW throughout the White House review process, and is in line for a healthy commission if it flies. <p>
What about DPW? The emir-controlled corporation manages a vast network of ports across the globe (revenues from U.S. operations would comprise less than 10 percent of their annual income). They are, for example, partnering in a $50 billion port-and-resort-city construction project near Jeddah in Saudi Arabia right now. <p>
And Dubai itself? It's the second-richest principality in the United Arab Emirates, a bastion of laissez-faire capitalism, and just as exploitative of its workers as our own plutocracy. <p>
Rightly seen, this is the Gulf States equivalent of another no-bid Halliburton or Bechtel deal. It should be investigated primarily for the stench of backroom corruption and kickbacks, not for the race or nationality of the players.<br>
<b>Bott Ikeler</b>, Providence, R.I.<p>
