<font face="Times New Roman, Times, serif" size="5"><b>Frame-up in Feds' "terror" trial</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 24, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">JUST HOW far will the Bush administration go to railroad Zacarias Moussaoui?<p>
Moussaoui is the only person to be charged in the U.S. in connection with the September 11 attacks. Although he admits to being an al-Qaeda member, he denies any involvement in September 11.<p>
Having earlier pled guilty to conspiracy, the question currently before the jury is whether to jail Moussaoui for life or sentence him to death as the Bush administration wants.<p>
At the time of the attacks, Moussaoui was in a Minnesota jail--having been arrested several weeks earlier on immigration violations. The Justice Department claims that, while not a direct participant, Moussaoui could have prevented 9/11--if he had told investigators that al-Qaeda wanted to fly planes into buildings. Prosecutors say that government aviation officials would have then tightened security and prevented the hijackings. <p>
Federal judge Leonie Brinkema brought the Moussaoui trial to an abrupt halt--and threatened to toss out much of the government's witness list or to take away the death penalty as a possible punishment--after it was revealed that Transporation Security Administration lawyer Carla Martin coached witnesses in the case.<p>
Martin apparently communicated with several witnesses by e-mail about how they should testify in order to boost the government's claim that Moussaoui's failure to disclose information led to the 9/11 attacks. She also sent portions of the court transcripts to at least seven witnesses--a clear violation of federal court procedure.<p>
Martin told the witnesses that government prosecutors had "created a credibility gap that the defense can drive a truck through." She was talking about the testimony of FBI agent Michael Anticev, who acknowledged under cross-examination that the bureau had known--long before before Moussaoui was taken into custody--of al-Qaeda plans to fly planes into CIA headquarters in Langley, Va., and into the Eiffel Tower. <p>
Martin suggested ways that the other witnesses could avoid Anticev's "mistake." "In all my years on the bench, I've never seen a more egregious violation of the rule about witnesses," Judge Brinkema said before suspending the trial for several days. <p>
According to lawyers coordinating private lawsuits for property damage from the September 11 attacks, Martin may not have been motivated solely by zealousness for the prosecution of a "terrorist." <p>
Apparently, Martin received the trial transcript from a United Airlines attorney--prompting speculation that the witness coaching was done to help airlines scared that the government's arguments in the Moussaoui case might undercut their defense against paying civil damages to 9/11 victims.<p>
Although Brinkema originally ruled that the government would not be permitted to call any aviation security witnesses, she partially reversed her ruling last week. Now, the government will be allowed to call "substitute" witnesses with no connection to Martin to testify about whether the federal government could have prevented the September 11 attacks if Moussaoui had disclosed what he knew.<p>
But new witnesses at such a late date will make it difficult for the defense to refute their testimony or find rebuttal witnesses.<p>
The Moussaoui case makes it clear: there's no such thing as a fair trial when the Bush administration for anyone labeled a terrorist by the Bush administration.<p>

