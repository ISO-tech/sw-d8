<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">April 28, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Oakland teachers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jessie Muldoon, Oakland Education Association</b></font><p>

<font face="Times New Roman, Times, serif" size="3">OAKLAND, Calif.--Just nine hours before teachers in the Oakland Unified School District were set to walk out on a planned one-day strike on April 20, union negotiators announced a settlement and canceled the strike.<p>
Rank-and-file activists in the Oakland Education Association (OEA) were surprised to hear that the strike was called off. Many of them had been preparing for the strike for weeks, making signs, readying their sites and arguing with coworkers to join the strike and picket together. Leaders of SEIU, AFSCME and the Teamsters had all pledged to support the strike.<p>
All signs indicated that the one-day strike would be extremely successful. In fact, even State Administrator Randolph Ward, who had pledged to keep the schools open with scab teachers, had to back down in the face of the strike. Ward canceled school on the day of the strike, claiming that he was concerned with student safety.<p>
But the feeling among teachers was that the replacement teachers didn't exist and that Ward didn't want to deal with the overwhelming community support for the teachers.<p>
Plans were made for student walkouts and parent strikes, and families were ready to march with OEA members on the picket lines. The OEA was in the position to go on the offensive and fight harder at the negotiation table, and to push ahead with the strike.<p>
Instead, OEA leaders canceled the strike and told members to go to work for a "voluntary professional development" day. Many members who hadn't got the news about the strike being cancelled showed up for non-existent picket lines.<p>
In the end, frustration about the strike, confusion about the tentative agreement, and general disorientation characterized the day.<p>
Details of the settlement are still emerging, but health care costs have always been at the heart of this contract fight. Oakland teachers are paid less than teachers in nearby districts and work in some of the worst conditions in the Bay Area. Fully covered health care has been one of the benefits that keeps teachers in Oakland.<p>
This contract, if ratified, ends fully covered health care, and opens the door for increased health care costs for years to come. In the 2006-07 school year, teachers will pay .5 percent of their salary toward health care costs, and the same in 2007-08. On the last day of the last year of the contract, union members will begin paying 4 percent of health care premiums.<p>
OEA members have worked under a 4 percent pay cut since 2003, and this contract claims to redress this pay cut. The proposal will total 6.25 percent salary increases, but the pay cut and the rising health care costs significantly undermine these numbers. Another concession includes increasing the counselor-to-student ratio from 1:500 to 1:700.<p>
Many members were hurt that the president notified OEA members that the strike was off with an announcement on the local TV news.<p>
By canceling the strike, OEA leaders missed an important opportunity to build unity and solidarity. Several activists argue that the OEA could have continued with strike, put up a fight, and held out for more.<p>
As details of the proposed contract emerge, OEA members will have to decide whether to accept a concessionary contract or vote no, and prepare to fight for more.<p>

