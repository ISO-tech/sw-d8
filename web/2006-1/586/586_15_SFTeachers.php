<font face="Times New Roman, Times, serif" size="5"><b>"Why I'm voting no on S.F. teachers' contract"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 28, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">ON APRIL 10, the bargaining team of United Educators San Francisco (UESF) came to a tentative agreement with the San Francisco Unified School District (SFUSD) on our contract. <p>
Strike preparation was called off just as members were getting prepared for their first picket captain training. The proposed contract contains a significant concession on the vesting period for health care for paraprofessional staff and a raise that barely keeps pace with inflation.<p>
UESF member ADRIENNE JOHNSTONE sent this letter to <i>Socialist Worker </i>about why she's planning to vote against the tentative agreement.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear brothers and sisters,</b> <br>
I'm writing to express why it will be important for us to register a sizeable "no" vote on our tentative contract agreement.<p>
There are good things in this tentative agreement. Among them, paraprofessionals have gained some parity (in treatment though certainly not in salary) with teachers, and the salaries of the pre-1986 child development professionals have finally been unfrozen. Importantly, we stopped the district from making severe cuts to working conditions.<p>
But for many educators I know, the 8.5 percent "raise" is a bitter pill to swallow after two years with no contract. It is not lost on our brothers and sisters that though the contract expired in 2004, we will only get a tiny 2 percent raise retroactive back to July 2005. <p>
For teachers hired after 1993, we are being asked to wait until April 2007 to see the 8.5 percent raise in our check. Even then, with taxes and higher gas prices and increases in health care costs, we will be lucky to take home about what we did at the time of our last raise four years ago.<p>
We were ready to strike for what we deserve, weren't we? Half of us came out and voted by an 87 percent margin to do just that. At our schools, we were talking about picket duty and supporting each other in hard times. <p>
We should have been more aggressive. We should have pushed the district as far as we could. We should have threatened to go out during standardized testing for a 10 percent raise. Changing the vesting period for paraprofessional health care is a concession we shouldn't accept.<p>
We know that the public supports teachers. We've seen study after study that indicates the public wants better-paid teachers and would even vote for tax increases to see San Francisco teachers get better raises. We should vote no on this contract to send an early warning to SFUSD that we'll back and expecting more.<p>

