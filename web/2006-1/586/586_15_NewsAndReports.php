<p><a name="Pat"></a><font face="Times New Roman, Times, serif" size="4"><b>Protest Pat Buchanan</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Grieco and Matt Richman</b></font><p>

<font face="Times New Roman, Times, serif" size="3">EWING, N.J.--Reactionary political figure Pat Buchanan was paid $15,002 to speak last week at the College of New Jersey, but the ultra-conservative's arrival was greeted with a protest of more than 50 students outside the lecture hall--far more than those who showed up to support him.<p>
Many protestors wore black in a show of solidarity, while others held up pro-immigration signs. Several times during the 40-minute rally, the group chanted slogans like, "Racist, sexist, anti-gay--Pat Buchanan, go away!" while security guards and a handful of College Republicans looked on.<p>
To mobilize for the protest, activists papered campus with fliers featuring some of Buchanan's "best" racist and homophobic quotes.<p>
Buchanan's lecture was mainly an indictment of the Bush administration for not being "conservative" enough. He hit on topics like the war and the economy, but saved his most virulent remarks for immigration.<p>
During the question-and-answer period, the majority of students asked pointed and confrontational questions to expose Buchanan's bigotry. It was in this segment that he railed against "political correctness" and quoted the infamous "Bell Curve" study to suggest racial IQ differences.<p>
The event was a major success in bringing together a more vocal and unified left at the college. <p>

