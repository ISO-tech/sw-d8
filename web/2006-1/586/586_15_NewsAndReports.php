<p><a name="Bush"></a><font face="Times New Roman, Times, serif" size="4"><b>Protest George Bush</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jenny Olsen</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SACRAMENTO, Calif.--A few thousand people converged in front of the California Fuel Cell Partnership Building here to protest President Bush on April 22.<p>
A day earlier, a crowd of 1,000 students and community members forced Bush to move his meeting with some Stanford professors off campus to the private residence of former Secretary of State George Schultz.<p>
Bush arrived in Sacramento for an Earth Day photo opportunity to promote alternative energy sources to oil. Protesters gathered to expose the hypocrisy of the president's call for environmental saving policies while waging a destructive war that is killing an entire country's ecosystem. <p>
"Coal is not an alternative resource and there is no alternative to human life," said Ellen Mitchell, a member of Code Pink and Davis Students Against the War. Protesters carried signs reading, "Go solar, not ballistic," and "U.S. out of Iraq, Bush out of West Sac." Protesters also made the connection between Bush's war on Iraq and the war on immigrants in this country. <p>
Organizers encouraged people to join in the May 1 national boycott and protest against anti-immigrant legislation at the Capitol.<p>
During the Stanford protest, the crowd chanted, "Hey-Hey-Ho-Ho-Bush is here, he's got to go." At one point, the police tried to clear the streets, but students refused to comply and 50 more cops in full riot gear were called in.<p>
The event inspired many onlookers, including many prospective students visiting campus for the first time. "I thought Stanford was really a bubble, but seeing all the people that are out here has shown me that people really are concerned," said incoming freshman Alejandra Aponte. "I'm from Guatemala. Right now, President Bush is doing some very interesting things in Latin America. We have a phrase, 'For every pig, there will be a Saturday.' Basically, his game is over."<p>
