<font face="Times New Roman, Times, serif" size="4"><b>Prosecute police torture</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Noreen McNulty</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--The Campaign to End the Death Penalty and other organizations brought together more than 70 people to demand the prosecution of a former police officer charged with overseeing the torture of more than 130 Black men.<p>
The event also marked the fourth anniversary of the appointment of special prosecutor Edward Egan, who was assigned to investigate allegations of police torture by Chicago police officers under the command of former Lt. Jon Burge. <p>
Outside the special prosecutor's office, activists chanted "Jail Jon Burge" and "Justice delayed is justice denied" while community leaders spoke to the press--including Rev. Calvin Morris of the Community Renewal Society; Larry Kennon, an attorney and petitioner for the appointment; and Greta Holmes of the Campaign to End the Death Penalty. <p>
"Bring the perpetrators of these acts to justice," read an open letter delivered to Egan's office calling on Egan to indict the police officers involved. "The perpetrators should be punished and the victims deserve freedom or new trials." After four years of investigation, "The question now is whether or not this appointment was for the purpose of covering up," said Kennon.<p>
For years, police on the South Side of Chicago tortured suspects into coerced confessions. More than 130 victims of torture--all African American--endured electro-shock, severe beatings, suffocation, and/or burns at the hands of Burge and the racist cops under his command. <p>
To this day, many of their victims remain behind bars, wrongfully convicted and imprisoned. Some were even sent to death row--four men were pardoned and others on death row had their sentences commuted to life by former Gov. George Ryan in 2003.<p>
"Jon Burge is free," said Greta Holmes. "He lives in Florida, and he's enjoying life. The victims who he tortured are still in jail, and this is not justice. Justice delayed is justice denied, and we want justice now."<p>
