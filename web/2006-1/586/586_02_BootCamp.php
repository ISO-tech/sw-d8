<font face="Times New Roman, Times, serif" size="5"><b>Victim of a Florida boot camp</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 28, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">SOME 2,000 people marched on Florida's state Capitol building April 21 to demand justice for a Black teenager who died in January after being beaten by guards at a youth boot camp.<p>
Revs. Jesse Jackson and Al Sharpton joined the protesters--many of them students from Florida State University and Florida A&amp;M University--in chants of "Justice delayed is justice denied." The night before, about 30 students participated in a 33-hour sit-in at the governor's office.<p>
On the day of his death, 14-year-old Martin Lee Anderson was restrained, kicked and beaten by guards at the Panama City boot camp for half an hour--all shown on videotape.<p>
Originally, the medical examiner ruled that the cause of death was complications from sickle cell trait. Anderson's family is waiting for the results of a second autopsy. So far, no one has been charged.<p>
The night before the protest, Florida Department of Law Enforcement (FDLE) head Guy Tunnell resigned, in large part because of remarks he made about Jackson and Sen. Barack Obama (D-Ill.), calling them "Jesse James" and "Obama bin Laden." The FDLE had already been taken off the investigation into the teen's death after it was found that Tunnell, a former sheriff who started the boot camp, e-mailed the current country sheriff who runs the camp to complain about critics.<p>
"Thinking about my baby, Martin, with him up looking down at me, I know he knows Mommy's fighting," Martin's mother, Gina Jones, told the <i>Miami Herald.</i> "I'm going to continue on fighting for Martin. He doesn't have a voice. I'm Martin's voice. I'm going to speak for Martin."<p>
Protesters are fed up with waiting for Florida politicians to do the right thing. "Governor Jeb Bush, Attorney General Charlie Crist, State Attorney Mark Ober, we are waiting," Florida State University student Gabriel Pendas told reporters. "We are watching, but not for long."<p>

