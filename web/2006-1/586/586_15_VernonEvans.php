<font face="Times New Roman, Times, serif" size="4">End the death penalty in Maryland</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Justice for Vernon Evans</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Virginia Harabin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 28, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">BALTIMORE--Activists against the death penalty are planning a march in support of Vernon Evans, who won a stay of execution in early February from the Maryland Court of Appeals. <p>
A three-day march sponsored by a coalition of groups opposed to the death penalty will begin on May 6 at the Supermax prison in Baltimore and culminate with a demonstration outside the Annapolis courthouse on May 8. <p>
On the morning of May 8, the court will hear Vernon's claims on several issues, and its decision will determine when and if Maryland will resume executions. Vernon won a stay just hours before he was scheduled to be executed in early February, effectively halting all executions in Maryland. <p>
This week, Lt. Governor Michael Steele, a Republican, hinted that he would support a moratorium and cited eight concerns that merit further study--including false confessions, the use of jailhouse informants, ineffective assistance of counsel, and racial, economic and geographic disparities.<p>
Debate about the death penalty in Maryland has been growing more intense since the execution of Wesley Baker in December. In a civil suit, Vernon is challenging Maryland's protocols for lethal injection, which have never been subjected to public review and may be in violation of state law. <p>
The court will also hear Vernon's appeal charging racial bias in death sentencing, as documented in the 2003 University of Maryland study that found that death sentences were more likely to be imposed in cases where the victim is white and the defendant Black, and if the murder happens in Baltimore County. The court will also consider whether the jury that sentenced him to death should have heard about his troubled childhood, which included physical and sexual abuse and a suicide attempt at age 10. <p>
An outspoken activist against the death penalty, Vernon encouraged the demonstrators. "The death penalty has been proven to be unfair," said Vernon. "This society almost executed over 100 innocent people in your name. You cannot continue to allow this sort of injustice."<p>

