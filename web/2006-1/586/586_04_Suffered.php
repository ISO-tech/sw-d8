<font face="Times New Roman, Times, serif" size="4">Undocumented and standing up for immigrant rights:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"I don't want others to suffer what I have"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 28, 2006 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">JOSE (not his real name) is a 23-year-old undocumented construction worker in Orange County, Calif. He talked to KARL SWINEHART about his experience coming to the U.S. <p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">IN MEXICO, I worked helping a karate instructor and working weekends as a waiter at a club. I made the decision to come to the U.S. because my dad had been here for six years, and it had been a long time since I'd seen him. I saw how hard it was for my parents to work because they're getting up there in years, and for that reason, I decided to come to help them.<p>
In Mexico, my dad was a construction worker, and my mom cleaned homes. My mom is still in Mexico. When my dad came, my mom couldn't work anymore. My grandma got sick in Oaxaca, and the medical care isn't the same there, so my mom left her job. <p>
My dad had already come here. He came because he wanted a house for us. Everybody's dream in Mexico is to have a house. He wanted a house and something better--that's why he came. And one year later, my grandma got sick. My parents have gone eight years without seeing each other, but they talk on the phone two or three times a week.<p>
I ended up in Tijuana for a while, because I have a cousin there, who I stayed with. I met a <i>coyote</i>. From there we went to a place on the Colorado River, and we crossed the border. Sixteen people crossed. I feel lucky because I didn't have to cross long distances, I didn't spend days in the desert. It was a 10-hour trip with 16 people from Yuma, Arizona, to Los Angeles, all of us in the back of a minivan.<p>
There are a lot of feelings people have when they come over. Some people come to reunite with family. A lot of people like me come with a lot of illusions about having a better life. Those of us coming for the first time have big illusions in accumulating some capital to help our families. Unfortunately, in our countries, we don't get that opportunity.<p>
There are people who are calm, like me, but on the inside, we fear that something's going to happen to us on the way. A lot of things cross your mind when you're crossing the border.<p>
When I first got here, I didn't have work for a while, but my dad said we'd go to the factories. I got a fake ID, like a fake Social Security number, to get into the factories. I put in an application. That was the first day, and I said I was ready to work. <p>
The second day, I went to the labor center--a place where there are day laborers because my dad still didn't have work at that time. I went with him, and the first day I went there, there was a person who gave me work for just one day, painting. My dad found a job from there, laying tile, and he told me that I should come and help him. I learned a trade little by little, but not one trade in particular. <p>
A lot of people say that construction workers earn a lot of money, but it's a lie because there are times you work a whole week, and then there are other times where there's no work. One difference is that in construction, they pay us in cash, the work is hard and tiring. This is different than factory work, where they pay you with a check. Compared to factory work, day laborers don't have a way to prove that they've been paying taxes.<p>
We come thinking we're only going to be here a short time, and when we realize we have a life here, it's hard to fix the situation. There are things that are very expensive for us--rent is expensive, other expenses, and you have very little left. So with time, you get absorbed, the years go by, and you have a life here. <p>
I'd like to say that we immigrants came in search of better opportunities for our families. We never thought about wanting to take people's jobs or hurt people. We ask that people get to know us better and realize that we're not bad. We also care what's happening to people who are from here.<p>
I got involved in this pro-immigrant movement for the other young people like me, so that they can be with their dad at Christmas--so they can see him. I want other young people like me to have the opportunity that I didn't have. There are a lot of things that we have to work on together to defend civil rights, because I don't want other people to suffer what I've suffered. <p>

