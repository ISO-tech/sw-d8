<font face="Times New Roman, Times, serif" size="4">Al-Arian facing deportation</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The witch-hunt that never ends</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 28, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">AFTER 38 months unjustly imprisoned, former University of South Florida professor Sami Al-Arian reached a plea agreement with federal prosecutors in April.<p>
Arrested in February 2003, Al-Arian has spent the months since fighting charges that included racketeering, conspiracy and materially aiding "terrorists." He has faced deplorable conditions, including indefinite solitary confinement, restricted access to lawyers and family, and a lack of medical care.<p>
Al-Arian was accused--along with codefendants Sameeh Hammoudeh, Hatem Fariz and Ghassan Ballut--of using an academic think tank, a Muslim school and a charity as a cover for raising money for the Palestinian Islamic Jihad (PIJ), a group that the U.S. State Department has labeled a "terrorist organization."<p>
In December 2005, after a six-month long trial, a jury acquitted Al-Arian of eight of the 17 charges against him, including the most serious ones. It deadlocked on the other minor charges. Ballut and Hammoudeh were acquitted on all charges, while Fariz was found not guilty on 24 out of 32 counts, with the jury deadlocking on the remaining eight.<p>
But the witch-hunt hasn't ended. Even after the verdict, Al-Arian and Fariz were kept in prison while the government took months considering whether to retry them on the remaining lesser charges. And even though Hammoudeh was acquitted of all charges, agents from Immigration and Customs Enforcement (ICE) detained him immediately after the trial. He is currently waiting to be deported. <p>
Now, in order to end his ordeal, Al-Arian has pleaded guilty to a single count of the most minor charge--conspiracy to provide services to the PIJ. <p>
In exchange, Al-Arian will serve as much as eight more months in prison before being deported to a yet-to-be determined country. A Palestinian born in Kuwait, there is speculation that Al-Arian could be deported to Egypt, where he has family.<p>
"The decision to accept the product of these negotiations was made by the Al-Arian family as a whole," explains a statement from the Tampa Bay Coalition for Justice and Peace, the main support group for Al-Arian.<p>
"Their concern first and foremost was to end Dr. Al-Arian's suffering. Even after he spent two years in solitary confinement under harsh conditions in a federal penitentiary while awaiting trial, and following his acquittal on the most serious charges, Dr. Al-Arian's conditions of confinement have remained deplorable. His release will also reunite him with his youngest children, who have been traumatized by their father's absence in the past three years."<p>
Federal prosecutors last week claimed Al-Arian's plea as a major victory for the government's "war on terror." "Because of the painstaking work of the prosecutors and agents who pursued this case, Al-Arian has now confessed to helping terrorists do their work from his base here in the United States--a base he is no longer able to maintain," said U.S. Attorney Paul Perez, whose office prosecuted the case.<p>
But the fact remains that federal prosecutors were never able to convince a jury of Al-Arian's guilt on a single charge--despite investigating him for more than 10 years, and presenting more than 80 witnesses and hundreds of hours of taped conversations at his trial. On the nine charges on which the jury deadlocked, 10 of the 12 jurors wanted to find Al-Arian not guilty.<p>
And as Al-Arian's attorney Linda Moreno stressed to the <i>St. Petersburg Times</i>, "In the agreement, [Al-Arian] did not plead guilty to any crime of violence, and by pleading, he gave his family closure in this ordeal." <p>
Georgetown University law professor and civil liberties advocate David Cole said that the plea deal in reality represented "a major blow to the government." "This is someone they initially claimed was guilty of conspiring to kill and maim innocent civilians," Cole said. "The government essentially dropped its case against him."<p>
But while the deal isn't a victory for the government, it certainly isn't justice to force Al-Arian out of the country he has called home for the past 31 years.<p>


