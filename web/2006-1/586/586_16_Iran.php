<font face="Times New Roman, Times, serif" size="5"><b>No war on Iran!</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 28, 2006 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">"ALL OPTIONS are on the table." That was George Bush's response last week, when asked if the U.S. was considering military action against Iran.<p>
The Bush administration claims that the Iranian government's push to develop nuclear technology--and its recent announcement that it has successfully enriched uranium--warrant a hard-line response, including threats of sanctions and potential military strikes.<p>
But as recent expos&eacute;s by investigative journalist Seymour Hersh and others make clear, the Bush administration has been looking for an excuse to attack Iran for a while.<p>
"The Bush administration, while publicly advocating diplomacy in order to stop Iran from pursuing a nuclear weapon, has increased clandestine activities inside Iran and intensified planning for a possible major air attack," Hersh wrote earlier this month. "Current and former American military and intelligence officials said that Air Force planning groups are drawing up lists of targets, and teams of American combat troops have been ordered into Iran, under cover, to collect targeting data and to establish contact with anti-government ethnic-minority groups," he added.<p>
The air strikes could utilize bunker-buster nuclear weapons, according to Hersh. That means nukes targeting Iran's main centrifuge plant at Natanz, 200 miles south of the capital of Tehran, with a population of 12 million people.<p>
Despite its admitted dismal performance in the run-up to the invasion of Iraq, the U.S. media has again been all too willing to whip up war hysteria over Iran.<p>
"Iran could produce nuclear bomb in 16 days, U.S. says," screamed one recent headline. That claim, drawn from remarks by Stephen Rademaker, U.S. assistant secretary of state for international security and nonproliferation, is absurd, say experts.<p>
The 16-day estimate is based on Iran moving to "industrial scale" uranium enrichment and a 330-fold increase in production. Under current conditions, it would take, Rademaker admitted, more than 13 years for Iran to produce enough highly enriched uranium to build a nuclear weapon.<p>
Nevertheless, the Bush administration is pushing the United Nations Security Council to approve sanctions on Iran that would include freezing assets, a travel ban against government officials, and a tightening of export controls--the same kind of measures proposed for Iraq to build momentum for the 2003 invasion.<p>
But the U.S. is getting a less-than-enthusiastic response from Security Council members China and Russia.<p>
To China, Iran is an important oil partner--it has a $70 billion oil development project going in Iran. Russia, meanwhile, is in the process of building an $800 million nuclear power plant in the southern part of the country and is due to complete the sale of surface-to-air missiles to the Iranian government.<p>
Still, if the UN won't do Washington's bidding, then the Bush administration has proved its willingness to take action on its own. That's why it's clear that the real "nuclear threat" in the world today isn't Iran, but the U.S. government.<p>

