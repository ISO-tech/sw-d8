<font face="Times New Roman, Times, serif" size="5"><b>TWU faces unjust fines</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sarah Wolf</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 28, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Ten days of citywide labor rallies and vigils will mark the jailing of Transport Workers Union (TWU) Local 100 president Roger Touissant, who N.Y. Supreme Court Judge Theodore Jones sentenced to 10 days in prison on April 10. <p>
The judge later fined the union $2.5 million and took away its right to automatic dues check-off as punishment for the union's three-day strike in December. The strike was illegal under the state's anti-union Taylor Laws, which ban work stoppages and slowdowns by public employees. <p>
Toussaint called the ruling "unfair, excessive and political" and said that he would appeal the financial penalties. He chose to begin his jail term April 24 after a march of about 2,000 TWU members and other union activists across the Brooklyn Bridge. <p>
After the ruling, other union officials pointed out that the penalties imposed are enough to bankrupt the union. The New York City Central Labor Council and state AFL-CIO are endorsing and publicizing this week's actions, which include vigils at the jail where Toussaint will be held and participation in the April 29 antiwar march.<p>
Toussaint's jailing comes after around 70 percent of union members approved the same contract the local rejected following the strike--a move that one member, train operator John L., called "a step back for the movement."<p>
Although the contact included some victories, such as stopping the Metropolitan Transit Authority (MTA) from forcing employees to accept broader job categories and forcing new workers to pay for their pensions, participants in the no-vote campaign in January pointed out that the contract's addition of a 1.5 percent employee contribution to the health plan would set a dangerous precedent for future concessions and would more than offset the contract's apparent gains. <p>
Toussaint, who pushed for the re-vote, is hoping that the successful re-vote will influence the outcome of ongoing negotiations. The MTA is pushing for negotiations to move to binding arbitration, under which both parties would be forced to accept the ruling of a supposedly neutral third party. <p>
But according to a recent leaflet by Transit Workers for a Just Contract (TWJC), a group of dissident union activists that formed in the wake of the no-vote campaign, the push for arbitration could be a bluff on the MTA's part. The leaflet points out that, among other factors, such arbitration could take the MTA's $1 billion surplus into account and force it to offer a higher wage increase than it offered in the rejected contract. <p>
Rather than push for an unsatisfactory deal, TWJC argues, Toussaint could have accepted a months-long arbitration process in order to buy time--and then organize the membership to "renew the fight" on the ground. Ultimately, the credible threat of another strike could push the MTA to agree to a better contract.<p>

