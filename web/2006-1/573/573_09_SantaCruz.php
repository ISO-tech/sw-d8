<font face="Times New Roman, Times, serif" size="5"><b>The protest they called a "credible threat"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 27, 2006 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">IN DECEMBER, NBC News revealed that the Pentagon has been spying on groups that it considers a possible "threat" to national security. <p>
On the more than 400-page-long list are student antiwar protests against military recruiters at eight campuses. A protest organized by Students Against War (SAW) at the University of California (UC)-Santa Cruz was deemed a "credible threat" to national security.<p>
ALESSANDRO TINONGA, a student at UC-Santa Cruz and member of SAW, describes the event that landed SAW on the government's list, and the reaction of students to the news.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">RECENT REVELATIONS that the Pentagon has been spying on activist groups across the country have sent out shock waves. <p>
At the UC-Santa Cruz, in particular, activists were amazed to find out that among the 1,500 "suspicious incidents" cataloged in the document, a counter-recruitment action that took place on April 5, 2005, was the only one where a university group was labeled a "credible threat."<p>
Inspired by mass actions at Seattle Central Community College and San Francisco State University that successfully kicked military recruiters off campuses, SAW spent weeks organizing for a major mobilization to oppose the presence of military recruiters at a career fair that was sponsored by the university. <p>
Opening with a rally in the Quarry Plaza, located at the center of campus, students from SAW, Revolution Youth, the International Socialist Organization and the Brown Berets exposed the hypocrisy of allowing military recruiters on an open campus. By the rally's end, more than 200 students had gathered in the plaza to participate in the action.<p>
Chanting "Racist, sexist, antigay, hey recruiters go away," the march forced open the doors of the Stevenson Event Center, where the fair was held, allowing almost 100 students to go inside. <p>
Demonstrators surrounded tables of Army, Navy and Marine recruiters, blocking them and yelling chants. Outside, more than 100 demonstrators blocked the main entrance and banged on the windows, demanding that the recruiters leave. <p>
Effectively, the career fair was shut down, and the military was unable to recruit. The military representatives left with a police escort, and without recruiting a single recruit. <p>
"It makes a lot of sense that the Pentagon would spy on us," says Hector Agredano. Agredano pointed out that in the weeks following the counter-recruitment action, students shut down the entire campus in solidarity with striking AFSCME workers--and that police used violent suppression against students participating in "Tent State University," a protest against cuts in higher education and the mammoth military budget. <p>
"The government needs to intimidate us and repress us because when people organize they make a difference," said Agredano.<p>

