<font face="Times New Roman, Times, serif" size="5"><b>Handbook on neoliberalism</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 27, 2006 | Page 10</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>PETRINO DiLEO reviews Haymarket Books' republication of <i>Your Money or Your Life: The Tyranny of Global Finance, </i>by Eric Toussaint, president of the Committee for the Abolition of Third World Debt.</b><p>

IN COCHABMBA, Bolivia, activists toil to prevent the privatization of gas and water. Throughout Africa, people have pleaded with the West to allow generic production of AIDS medications to help curb the continent's epidemic. Social movements in Latin America have crippled right-wing governments and elected leaders who openly challenge the West, like Hugo Ch&aacute;vez in Venezuela.<p>
What links these seemingly disparate struggles is that lined up on the other side in every case are the main institutions dictating neoliberal economic orthodoxy: The World Bank, International Monetary Fund (IMF) and World Trade Organization (WTO).<p>
Eric Toussaint's <i>Your Money or Your Life </i>provides an excellent framework for understanding these institutions of global capital and the neoliberal model. <p>
He explains how they've greased the wheels for multinational corporations and pushed forward the West's economic interests around the globe. The book shows how debt has been used as a weapon against the Global South--and documents how people have fought back in the past, and what those fights look like today.<p>
Toussaint's basic thesis is that World Bank and IMF programs have worked out in much different ways than advertised. The neoliberal premise is that developing nations need to modernize industry and agriculture with the aid of loans from the West. In return, countries that get development loans are required remove "barriers" to trade--such as environmental regulations or labor laws, decrease government spending and open up to international investment. <p>
This process is supposed to promote economic growth through letting these countries compete on the global stage.<p>
But the opposite has occurred. Most economies in developing nations have been made weaker under neoliberalism. <p>
Local industry has been restructured to focus on exports of raw materials and agricultural products, rather than finished goods--even though over the last 30 years, the price of such materials has fallen. With interest rates on the rise, developing nations have been forced to take on new and bigger debts at higher rates in order to pay back old ones.<p>
The net result is that Third World debt has ballooned--even though Third World countries have paid $4.6 trillion in repayments since 1980. At the end of 2002, less developed countries owed $2.4 trillion in debt, four times the 1980 figure of $580 billion. <p>
In all, developing nations now spend more to service their debts than they do on education, health care and social welfare combined. <p>
One strength of Toussaint's book is that it does not echo the argument made by many in the antiglobalization movement--that multinational corporations have risen in power independent from the power of the state. <p>
Toussaint describes globalization as a process that accelerates the concentration of capital in the hands of a few hundred companies, but he also says, "This process should not be exaggerated. Competition between MNCs [multinational corporations] is intense, and none is in a position to have a global monopoly. Furthermore, MNCs have not shaken themselves free of national states...They continue to rely on the state of their country of origin."<p>
The book is marred by proposals for reforms where Toussaint does not go into detail about how they should be implemented. While some are mild and could be carried out under the current system (such as a 0.1 percent tax on financial transactions), others would seem to require  mass struggle, if not revolution (for example, a mass transfer of wealth from the richest to the poorest countries, and the complete dismantling of the IMF, World Bank and WTO).<p>
But this book is unsurpassed as a handbook on international finance and neoliberalism, and the new edition from Haymarket is welcome.<p>

