<font face="Times New Roman, Times, serif" size="4">Antiwar author William Blum on the Osama bin Laden tape:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"The American empire needs to be corralled"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 27, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">WILLIAM BLUM is known to antiwar activists as the the author of several excellent history books documenting the crimes of U.S. imperialism. But last week, Blum's name was all over the media after Osama bin Laden recommended one of Blum's books in his latest taped message, the first to emerge in over a year.<p>
<i>Socialist Worker </i>contributor DAVE ZIRIN, author of <i>What's My Name, Fool? Sports and Resistance in the United States </i>and a <i>Nation </i>magazine columnist, talked to Blum about his reaction.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">LAST WEEK, during his much ballyhooed audio message, Osama bin Laden gave an Oprah-style book recommendation of <i>Rogue State </i>by William Blum.<p>
"If Bush decides to carry on with his lies and oppression," bin Laden said, "then it would be useful for you to read the book <i>Rogue State</i>, which states in its introduction: 'If I were president, I would stop the attacks on the United States: First, I would give an apology to all the widows and orphans and those who were tortured. Then I would announce that American interference in the nations of the world has ended once and for all.'" <p>
Actually, the quote, while written by Blum, is not in fact in the <i>Rogue State </i>introduction. Nevertheless, sales of <i>Rogue State </i>from Amazon.com shot up, climbing literally overnight from number 209,000 to 18--ahead of such mass-market best-sellers as <i>The Da Vinci Code </i>and the collected works of John Grisham. <p>
The 72-year-old Blum was also catapulted from obscurity to a mini-cause celebre, with interviews on CNN and articles in the <i>Washington Post </i>and <i>New York Times</i>. <p>
The publicity allowed Blum to get a hearing for the content of <i>Rogue State</i>, a brilliant detailing of U.S. intervention throughout the world that painstakingly documents coups fomented, death squads trained and funded, and dictators supported. Blum's explanation of how the U.S. undermines democracy and democratic movements (many of which bin Laden himself would oppose) was a total contrast from what the media usually talk about. <p>
Asked how he felt being recommended by bin Laden, Blum said, "Osama bin Laden and I happen to share an intense dislike for certain aspects of U.S. foreign policy and a liking for a certain book of mine. The American Empire needs to be corralled before it kills again, and bombs and tortures. If his 'endorsement' helps me to get my message to a lot more people, why should I reject it? The more people who are turned off by the empire's crimes, the sooner the empire will fall."<p>
Blum's welcome of the bin Laden's endorsement caused CNN's Wolf Blitzer to gasp--as did Blum's analysis that the roots of the September 11 attacks lie in the deadly foreign policy of the U.S. government.<p>
Yet Blitzer didn't ask Blum about his extensive documentation of how bin Laden is a creature of U.S. Cold War policy--a CIA-funded reactionary from a ruling Saudi family who served U.S. interest well in leading the guerrilla struggle in the 1980s against forces of the former USSR after its invasion of Afghanistan.<p>
Asked his opinion of this side of bin Laden's history, Blum repeated his anger at U.S. policy that buttressed the Taliban regime in Afghanistan. "My dislike of religious fundamentalism and oppression of women is second to no one," Blum said.<p>
The skyrocketing sales of <i>Rogue State </i>show how sincere people are about trying to understand why the U.S. is in what Dick Cheney calls a "generational war on terror." Millions of people are hungering for an explanation that goes beyond anti-Muslim racism or the idea that we are fighting "madmen" who appeared out of thin air. Those are answers that the politicians in Washington aren't willing or able to provide.<p>

