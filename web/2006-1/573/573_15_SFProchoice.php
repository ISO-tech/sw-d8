<font face="Times New Roman, Times, serif" size="5"><b>San Francisco activists confront anti-choice marchers</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Arturo Sernas</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 27, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Some 800 pro-choice demonstrators came out on January 21 to rally for reproductive rights and protest the second annual "Walk for Life."<p>
Walking under the banner "Abortion hurts women," the anti-choice marchers descended upon the city on the 33rd anniversary of the U.S. Supreme Court's ruling to legalize abortion.<p>
A variety of pro-choice groups lined the march route, including the Bay Area Coalition for Our Reproductive Rights (BACORR), Code Pink, Raging Grannies, the International Socialist Organization, Radical Women and World Can't Wait.<p>
Unfortunately, the mainstream pro-choice organizations like Planned Parenthood, NARAL and NOW refused to mobilize their memberships and ignored the bigots, opting instead for a vigil later in the evening. As a result, the pro-choice forces were heavily outnumbered.<p>
Nonetheless, pro-choice demonstrators assembled along the sides of the march, chanting "Bigots go home!" and "Keep your rosaries off our ovaries!" At one point, demonstrators tried to block the march route, but were quickly pushed back onto the sidewalk by San Francisco police and threatened with arrest.<p>
In November, California voters defeated Proposition 73, a parental notification law--a victory for pro-choice forces in the state. But the margin of victory, 53 to 47 percent, was slim.<p>
Pro-choice sentiment is widespread--but it must be organized. We need to confront the anti-choice zealots and put the fight to defend women's rights back in the streets where those rights were won in the first place.<p>

