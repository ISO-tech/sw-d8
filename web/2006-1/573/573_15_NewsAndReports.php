<p><a name="MLK"></a><font face="Times New Roman, Times, serif" size="4"><b>Remember Martin Luther King</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Lonnie Lopez</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--More than 2,000 people packed the Garfield High School gymnasium to celebrate the life and legacy of Dr. Martin Luther King Jr. <p>
Now in its 23rd year, the event culminates in one of the largest and most diverse political events in the Pacific Northwest. Dozens of local grassroots, labor, minority and progressive community organizations and volunteers make up the Martin Luther King Celebration Committee that organizes the event.<p>
In the morning, local leaders presented workshops on racial inequality, economic justice and the antiwar movement. Teacher Jesse Hagopian; president of the Garfield High School Parent, Teacher, Student Association Amy Hagopian; and student Anna Hagopian spoke on the counter-recruitment movement and the launching of a Seattle College Not Combat initiative to a interested and diverse crowd of more than 50 parents, teachers, students, and grassroots community activists. <p>
The thousands in the gymnasium joined another 500 outside in the drizzle to march almost two miles to the Federal Building downtown. The theme for this year's rally and march was "Racism, poverty, war: Iraq, Katrina, no more!" The counter-recruitment movement also held a prominent place in the march as the parade route passed a local recruiting station.<p>
At the rally, speakers called on county officials to replace the image of the imperial crown on the county seal with an image of Dr. King, the county's namesake.<p>
