<p><a name="End"></a><font face="Times New Roman, Times, serif" size="4"><b>End executions</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Justine Prado</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN QUENTIN--One minute past midnight on January 17, more than 500 people stood outside the gates of San Quentin State Prison as the state took the life of 75-year-old Clarence Ray Allen. <p>
It was only one month earlier that many of these students, activists and religious leaders were at the prison to protest the execution of Stan Tookie Williams. <p>
At the time of his death, Allen was in very poor health--legally blind, suffering from diabetes and confined to a wheelchair. The recently scheduled February 21 execution date for death row prisoner Michael Morales led one woman in the crowd to say, "They're going to try and turn California into another Texas!"<p>
Barbara Becnel, close friend and advocate of Stan Tookie Williams, told the crowd that Gov. Schwarzenegger is a "coward for what he did to Stan, and for what he is doing to Clarence Ray Allen...We do not give our votes away and our power away, be it to Schwarzenegger or Democrats." <p>
Michael Wyman, Green Party candidate for State Attorney General, said, "We don't just need a moratorium on the death penalty. We need a moratorium on building new prisons."<p>

