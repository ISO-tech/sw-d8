<font face="Times New Roman, Times, serif" size="4"><b>Stop the school closings</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Somerset Stevens</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--About 600 parents, educators and students packed the Board of Education meeting January 19 to oppose the closure of their neighborhood schools. They pointed out how the schools slated to close were disproportionately in communities of color and working-class areas. <p>
The mass attendance of the board meeting was mobilized in less than three weeks.<p>
Ann Coulton, an activist and parent, explained that about 15 percent of relocated students from past closures never return to school. She also said the board claimed that the demographics would "sort themselves out."<p>
Community members were allowed minute-long remarks before being cut off--to which the crowd responded with jeers, hisses and shouts of "let her speak." "You see real estate, we see schools," said grandparent and arts educator Meagan Bierman. "These children are not data. They are people." <p>
Amos Brown of the NAACP, like many speakers, compared the closings to school segregation. Others took issue with the school board asking each neighborhood to defend its school--rightly characterizing this as an attempt to pit communities, which are frequently settled along lines of race and national origin, against each other. <p>
The board published information about the closures in English only, despite the fact that San Franciscans speak a multitude of languages. Organizers of the community response printed their materials in three languages and sought translators for other languages before the meeting.<p>
The board voted in favor of some of the school closures, but activists pledge an ongoing fight against this callous attack on the city's working-class and poor residents. As one parent said, "The children of San Francisco are all our children."<p>
