<p><a name="Nazis"></a><font face="Times New Roman, Times, serif" size="4"><b>Stop the Nazis</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Lonnie Lopez</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--With three days' notice, more than 150 local activists gathered at the base of the Space Needle to protest a rally planned by Nazis from the Oregon-based National Socialist Movement. They are trying to organize a chapter in the Seattle area.<p>
The exact location of the rally was not identified on the Nazis' Web site, but resourceful local activists had narrowed the likely location down to two possibilities--the Space Needle and the state capital of Olympia, an hour to the south. Antiracist activists stood watch in these areas, prepared to meet them wherever they decided to show. <p>
When the Nazis failed to show at the Space Needle, the protest turned into a victory rally, with activists from several organizations chanting, "No Nazis, no KKK, no fascists in the USA!"<p>
In Olympia, however, a small group of 10 Nazis finally arrived with flags waving, but were met by almost 100 vocal and angry activists. After an hour, the Nazis rolled up their flags and were led to safety in the backs of police cars to chants of "Go home!"<p>
By preventing the Nazis from holding their planned rally and limiting their ability to organize in Washington's largest city, antiracist activists showed that building a unified front is an effective way to stop the spread of this kind of hate.<p>
