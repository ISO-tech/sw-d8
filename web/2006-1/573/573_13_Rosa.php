<font face="Times New Roman, Times, serif" size="5"><b>How we're taught the story of Rosa Parks</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Leela Yellesetty</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 27, 2006 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Herbert Kohl, <b><i>She Would Not Be Moved: How We Tell the Story of Rosa Parks and the Montgomery Bus Boycott. </b></i>New Press, 2005, 144 pages, $22.95.</font><p>

<font face="Times New Roman, Times, serif" size="3">FROM THE resegregation of U.S. schools to the government's racist response to Hurricane Katrina, the targeting of Arabs and Muslims and the right-wing backlash against Latino immigrants, it is clear that a new civil rights movement is urgently needed.<p>
To build this movement it is crucial that we uncover the true history of the civil rights movement, including the story of Rosa Parks. This is what Herbert Kohl sets out to do in his new book, <i>She Would Not Be Moved: How We Tell the Story of Rosa Parks and the Montgomery Bus Boycott.</i><p>
Kohl focuses on the way Parks' story is told to children. Pouring over hundreds of children's books and textbooks, Kohl exposes the many myths about Parks that are perpetuated.<p>
The most often repeated myth is the "Rosa was tired" story. In this rendition, Parks is just a poor seamstress who is too tired to move from her seat for a white passenger. Her action and arrest leads to the Black community spontaneously deciding to boycott the city's bus system. Just as easily, the boycott succeeds and the buses are integrated. <p>
Kohl sets the story straight. Rosa Parks was an activist long before she boarded the bus in 1955. She was active in the NAACP and attended sessions about desegregation at the Highlander Folk School.<p>
The incident that sparked the boycott was not the first time she refused to move from her seat, nor was she the first person to be arrested for doing so. In the months before her arrest, leaders in the Black community had been planning to organize a boycott and were waiting for the right opportunity--specifically, for someone to get arrested who was widely respected and could withstand the inevitable backlash.<p>
As soon as they heard of Parks' arrest, the Women's Political Council of Montgomery drafted up and printed thousands of leaflets announcing the boycott to the Black community. The boycott lasted more than a year before it succeeded.<p>
Kohl's book will be especially useful to teachers as it contains an appendix filled with ideas for lesson plans.<p>
His concluding paragraph beautifully makes the case for why teaching the real story is so important: "Rosa Parks, the humane activist, is challenging us today to do more than just memorialize the past but to act in the present and in the future. However, when the story of the Montgomery bus boycott is told merely as a tale of a single person, it leaves children hanging or searching for someone to follow, when they should be the actors...Of course, the idea that only special people can create change is useful if you want to prevent mass movements and keep change from happening. Not every child can be Rosa Parks, but everyone can imagine her- or himself as a participant in the boycott.<p>
"As a tale of a social movement and a community effort to overthrow injustice, the Rosa Parks story, as I've tried to retell it, creates the possibility of every child identifying her- or himself as an activist, as someone who can help make justice happen." <p>

