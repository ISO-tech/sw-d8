<font face="Times New Roman, Times, serif" size="4">Discontent over concessions in UAW rank and file</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Ford to cut 30,000 jobs</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 27, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">FORD MOTOR Co. announced that it would eliminate up to 30,000 jobs and close 14 plants in the next six years. The planned cuts amount to about a third of the 87,000 Ford workers who are members of the United Auto Workers (UAW), and the plant closings represent about 26 percent of Ford's total production capacity.<p>
The announcement comes just weeks after UAW leaders announced that its members at Ford had approved a new health care plan with Ford that included new premiums and higher co-payments and deductibles by a razor-thin margin of 51 percent. The deal amounts to a concession of $850 million annually.<p>
Ford workers around the country doubted the legitimacy of the result in a sign of the growing anger at the unwillingness of UAW leaders to mount any effective resistance to the ongoing assault on workers in the auto industry.<p>
Many workers pointed out in interviews with mainstream newspapers that it's nearly impossible to imagine how the deal was approved if some of the largest locals--for example, in Chicago, Louisville, Ky., Kansas City, Mo., and St. Paul, Minn.--had all rejected it.<p>
Even though Ford had signaled it planned a painful "restructuring," the scale of the cuts, on top of the massive health care concessions, felt like a kick in the teeth to UAW workers.<p>
The same day of the Ford announcement, Delphi workers held a protest outside Delphi headquarters in Troy, Mich., to protest the threat against their wages, benefits and pensions. Delphi has declared that it will enter bankruptcy court in the coming weeks and plans to use the proceedings as leverage to extract severe concessions from workers.<p>
The recently formed Soldiers of Solidarity network that brings together rank-and-file union militants at Delphi organized the informational picket. "We are prepared to strike," announced the leaflet for the event in bold letters. "Corporations are determined to pursue lower nonunion wages in this country and ship jobs overseas. We are determined to fight this ruthless plan to degrade workers and defraud taxpayers."<p>
The day before, Delphi rank-and-filers held a closed strategy meeting to talk about preparing for a walkout and to get down to "the brass tacks" of a work-to-rule campaign.<p>
In some places, the work-to-rule campaign has already begun. "Most in our department, and the department that supplies us are doing it," said a worker at a Delphi plant in Flint.<p>

