<font face="Times New Roman, Times, serif" size="4">UCLA graduate offers students money to spy on teachers</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A right-winger's witch-hunt</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 27, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">A FORMER right-wing student at the University of California-Los Angeles (UCLA) is grabbing headlines with a scheme to continue his witch-hunt against left-of-center professors.<p>
Andrew Jones, who graduated from UCLA in June 2006, is offering students up to $100 a class if they hand over notes and tapes of classes taught by professors he considers to be radicals.<p>
After getting fired from two short-lived jobs--including one as a researcher for right-wing guru David Horowitz--and filing suit against a third employer, Jones decided to launch an independent alumni association, with himself as the only employee. Returning to his old campus haunts, he has vowed to continue his campaign against "extremist" teachers who, he says, use their courses to "indoctrinate" students with left-wing ideas.<p>
Jones' appeal on his Web site reads: "UCLA STUDENTS: Do you have a professor who just can't stop talking about President Bush, about the war in Iraq, about the Republican Party, or any other ideological issue that has nothing to do with the class subject matter? It doesn't matter whether this is a past class, or your class from this coming winter quarter. If you help expose the professor, we'll pay you for your work."<p>
But Jones' record as a right-wing student activist shows that he's the "extremist." As leader of the campus Republicans, Jones organized an "affirmative action bake sale"--a favorite stunt of the right, with different prices for men and women, and whites and people of color--and he tried to disrupt at least one antiwar demonstration.<p>
"He made it a point to attack anything that resembled affirmative action or anything that, in his eyes, might serve students of color before other students," Anica McKesey, a former undergraduate student body president at UCLA, told the <i>Los Angeles Times</i>.<p>
Even his allies think Jones is a kook. Since graduating, he has been fired from two researcher jobs. One was with <i>U.S. News &amp; World Report's </i>conservative columnist John Leo. <p>
The other was for David Horowitz, self-appointed opponent of supposed left-wing bias on campus. Horowitz told the <i>LA Times </i>that Jones was fired for pressuring students to "file false reports on leftist students" at UCLA. The fanatical Horowitz called Jones "uncontrollable."<p>
"Ultimately, of course, this has nothing to do with me or my colleagues, or our teaching," Saree Makdisi, a UCLA English professor singled out on Jones' Web site, wrote in the <i>LA Times. </i>"The point, rather, is to silence voices that go against the zealots' right-wing orthodoxy, and to subject the classroom to outside political surveillance, not simply by vigilante groups like this one, but ultimately by the state itself."<p>

