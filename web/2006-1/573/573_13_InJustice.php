<font face="Times New Roman, Times, serif" size="4"><i>In Justice: </i>A new kind of TV crime drama?</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Innocent and behind bars</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Alice Kim</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 27, 2006 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>In Justice,</b> </i>starring Kyle MacLachlan, airs Fridays, 9 p.m. EST on ABC.</font><p>

<font face="Times New Roman, Times, serif" size="3">IT'S A crime drama--but instead of putting criminals behind bars, <i>In Justice</i> is about freeing the innocent.<p>
In the television world of cops and robbers, crime dramas almost always take the side of police and the prosecutors who are heroes just out to get the "bad guys." <i>In Justice</i> takes the side of the wrongfully convicted and reverses the formula that we see in <i>Law and Order</i> and <i>CSI.<p>
</i>These shows open with a gruesome, often gratuitous, crime scene that cries out for justice for the victims. Police, prosecutors and investigators--portrayed as crusaders for justice--then proceed to uncover the heinous criminals (never mind the rights of the accused) who committed the crime.<p>
Episodes of <i>In Justice</i> open with the statement, "This is what the jury believed." We then see the crime unfold--not how it actually happened--but "what the jury believed" in order to convict.<p>
The heroes of <i>In Justice</i> are the young defense attorneys and investigators who work for the National Justice Project in Oakland, Calif., (a fictional spin-off of real-life Innocence Projects that aim to exonerate the wrongly convicted) and uncover what really happened.<p>
The head of the Justice Project, David Swain--played by Kyle MacLachlan of the series <i>Twin Peaks </i>and HBO's <i>Sex and the City--</i>is a rich lawyer who gives up his $650-an-hour billings with corporate clients to work full time for the Justice Project.<p>
But Swain is hardly a cookie-cutter idealist. He's a media hound who wants to run for attorney general of California with questionable ethics whose irreverence is both amusing and offensive. <p>
Charles Conti, played by Jason O'Mara, is Swain's partner. He's an ex-cop who helped to put the wrong man in prison when he was on the force. In one episode, he tells one of his colleagues, "I got a man to confess to the rape and murder of his sister, and he didn't do it. DNA cleared him a year later. If he hadn't hanged himself in prison, he'd be free now."<p>
The defendant's suicide haunts Conti, who now uses his "insider's perspective" as a former cop to help solve his current cases. For example, when Conti learns that a teenage boy, who confessed to the crime of killing his sister, was interrogated by police officers for 15 hours, he can't accept that the confession was legitimate even though police and prosecutors repeatedly insist that "nothing illegal" took place.<p>
Scenes of the teen being interrogated by police for hours on end are true to life. The teen finally confesses to the crime out of sheer agony and exhaustion after an officer lies and tells him that his parents never want to see him again because they believe that he killed his sister. These perfectly legal interrogation tactics are put into question when Conti uncovers the real killer. <p>
In one episode, the Justice Project team finds that mistaken eyewitness testimony and sloppy police work were responsible for convicting the wrong woman for the murder of her father. In another episode, the Justice Project finds that an African American man was framed by corrupt officers for a robbery and murder that he did not commit. <p>
Unfortunately, in the real world, the wheels of justice turn much more slowly, and more often than not, they don't turn at all. Moreover, even as the show exposes the injustices of wrongful convictions, it resurrects the false notion that justice can be served by convicting the real criminals.<p>
Each episode not only frees the innocent but brings the guilty to justice, as stated in the title sequence. The formula of the television crime drama--this one included--focuses narrowly on a heinous crime, and then the heroes go about righting the wrong. <p>
But the real "wrongs" in our criminal justice system go beyond incarcerating the innocent. The tough-on-crime policies of the last two decades have put more than 2 million men and women behind bars, the majority of whom are African American and Latino.<p>
In the U.S., the reality of crime and punishment is based on inequality. Truth be told, there are two sets of laws in this country--one for the rich and one for the poor. As Eugene Debs, an early 20th century American socialist, said, "There is something wrong in this country, the judicial nets are so adjusted as to catch the minnows and let the whales slip through." This is the reality of "law and order" in the real world.<p>
Nevertheless, with the exonerations of hundreds of prisoners nationwide, including more than 120 people who were on death row, the airing of <i>In Justice</i> is a welcome acknowledgment of this growing trend.<p>

