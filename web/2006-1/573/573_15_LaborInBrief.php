<p><a name="VIS"></a><font face="Times New Roman, Times, serif" size="4"><b>Verizon Information Services</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Dominic Renda, shop steward, CWA Local 1105</b></font> <p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--The 300 Verizon Information Services (VIS) workers who produce the Yellow Pages phone books have been on strike since last October 31. The Communication Workers of America (CWA) called the strike because VIS insisted on the right to lower wages at any time during the life of a new contract--a demand that is plainly illegal, as the union points out.<p>
But this strike has been anything but easy. It has dragged on for two-and-a-half months--straight through the holiday season--due to the incompetence of the CWA leadership. <p>
The union has spent time enlisting "support" from Democrats and Republicans in the state senate and assembly. This support has amounted to politicians merely sending letters to Verizon urging a "swift and fair resolution to the strike."<p>
Meanwhile, the union set up picket lines that did not aim to stop scabs from entering worksites. And to make matters worse, the union leadership is virtually never at the picket lines. <p>
Perhaps the union's biggest mistake is that virtually no one outside of VIS knows that the strike is even happening. Not even non-VIS Verizon employees hear that much of the strike. There haven't been any union meetings among Verizon workers to raise support for our brothers and sisters at VIS.<p>
Due to all the strike's weaknesses, roughly 11 of the 75 workers are scabbing in New York, and picket lines have since completely disappeared at the VIS worksite here. <p>
But not all is lost. This strike can still be successful--but only if labor movement fundamentals, which are solidarity and effective picket lines, materialize in order to force VIS to bring about a "swift and fair resolution to the strike."<p>
