<p><a name="PI"></a><font face="Times New Roman, Times, serif" size="4"><b>Pratt Institute</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Cindy Klumb</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Secretarial and technical workers at Pratt Institute voted unanimously at a January 12 union meeting to give strike authorization to a negotiating committee. Out of 72 workers in the bargaining unit, 50 attended the meeting to cast their votes.<p>
Negotiations have been ongoing since the end of the summer, but little progress has been made. The union is asking for a one-time wage adjustment to make up for sacrifices made by its members in the 1990s to save the school from bankruptcy. For its part, Pratt is offering lower raises than the union has ever accepted before and demanding givebacks. <p>
Pratt had a $10 million surplus last year and several years of surpluses preceding this negotiation, yet administration officials have come to the negotiating table crying poverty.<p>
The clerical, secretarial and technical employees at Pratt are unable to continue to live without this adjustment--and are willing to strike if that's necessary to get a living wage from Pratt.<p>

