<font face="Times New Roman, Times, serif" size="4"><b>New York City home health aides</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Hannah Wolfe and Sean Petty</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--More than 1,000 home health aides and their supporters marched through the streets of Harlem January 16 in single-digit temperatures to demand that the various health aide contractors respect their union and pay at least $10 per hour in wages.<p>
The rally was organized by Service Employees International Union (SEIU) Local 1199, a health care workers union, which has been locked in battle with several home health care agencies for the last two years. Rev. Al Shapton and Local 1199 President Dennis Rivera attended the rally, which also commemorated the life of Dr. Martin Luther King Jr.<p>
Despite a three-day strike by home health workers in 2004, many agencies have yet to live up to their contract obligations and in several cases refuse to recognize the union.<p>
Non-union home health aides make between $6 and $8 per hour with no health coverage, which is absurd in a city with the highest cost of living in the country. In addition, many felt that it was upside down that the people who provide health care can't afford to get it themselves.<p>
"This is modern day slavery," said Imogene Lilly, a home health aide and union delegate at Sunnyside Healthcare, describing the conditions that she and her coworkers work under.<p>
Polly, before she joined 1199, had to pay for hospital and clinic visits related to diabetes out of pocket, while making only $6 per hour. "I have walked that road, no union, no benefits, so I'm here to support them," said Polly. "[Workers] need a living wage. At least $10 an hour, and even that's not enough. It's hard work."<p>
Many at the rally also took inspiration from the three-day strike by transit workers in December. "They opened a whole new world for all of the unions in this city to fight for what we need," said Imogene.<p>
