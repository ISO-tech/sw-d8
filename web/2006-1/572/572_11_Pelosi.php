<font face="Times New Roman, Times, serif" size="5"><b>Pelosi faces antiwar protest</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Josh On</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 20, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--More than 30 antiwar activists lined the front of the stage as Democratic Minority Leader Nancy Pelosi addressed a crowd of 1,000 at a town hall meeting on national security in the affluent Marina neighborhood.<p>
When it became clear that Pelosi would not address why she voted for the funding of the U.S. occupation of Iraq, protesters scattered throughout the audience made their way to the front and unfurled banners reading, "Bring the troops home now" and "Stop funding the war." Pelosi was visibly flustered by the protest and heckling but continued to answer a filtered set of questions from the crowd. <p>
The protesters came from a variety of organizations, including United For Peace and Justice, Code Pink, the Green Party and the International Socialist Organization.<p>
Many in the crowd were willing to listen to Pelosi's attempt to pass as an opponent of Bush's war, but were frustrated by her answers. For example, when asked if she would support a bill against building permanent bases in Iraq, she claimed that she didn't know if that was happening. In any case, she said, as far as the U.S. government is concerned no foreign bases are "permanent," even the ones in Germany. <p>
The most explosive moment was when she answered a question about the possibility of impeaching Bush. Once she made it clear that she would not support such an action and instead told the crowd to work for the Democrats in the 2006 elections, people from all over the room yelled their disbelief. <p>
They came to hear someone who shared their anger at the administration, but found it lacking in the diplomatic language of Nancy Pelosi. <p>
In 2004, San Francisco voters passed a resolution to bring the troops home now, and in November 2005, San Franciscans showed their opposition to military recruitment in schools and on campuses by voting for the College Not Combat proposition. <p>
Pelosi was quick to criticize the Bush administration's failures in Iraq, and she repeated the Democratic Party mantra of bad intelligence, lack of plans and no exit strategy, all the time reassuring her audience that she thought it was important that the U.S. has the most powerful military in the world. <p>
When it came to getting troops out of Iraq, she subscribes to Rep. John Murtha's "redeployment plan" and emphasized that the troops should be kept just over the horizon so they could be called upon when needed in Iraq. "[I support] strategic redeployment, positioning our troops in the region just over the horizon, with enough equipment in Iraq so we can go back in and deal with any threat," said Pelosi.<p>
It was clear to all that she was not talking about ending the war. Even in this affluent area at an event largely promoted among her most ardent supporters, Pelosi found herself to the right of her antiwar audience.<p>
<i>Todd Chretien and Ragina Johnson contributed to this report</i>.<p>

