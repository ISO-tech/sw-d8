<font face="Times New Roman, Times, serif" size="4">Author and activist Tariq Ali's radical memoirs</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A whirlwind tour of the 1960s</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Sarah Macaraeg</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 20, 2006 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Tariq Ali, <b><i>Street Fighting Years: An Autobiography of the Sixties. </b></i>Verso, 2005, 400 pages, $17.</font><p>

<font face="Times New Roman, Times, serif" size="3">THE WAR in Iraq and Hurricane Katrina, falling wages and job cuts, 37 million Americans living in poverty, a state-sponsored murder of peacemaker Stan Williams. If those who control our society demonstrated anything in the last year, through their greed, racism, negligence and hypocrisy, it was how utterly and completely unfit they are to rule--and how much we need to stand up and fight for an alternative. <p>
In <i>Street Fighting Years: An Autobiography of the Sixties, </i>veteran activist and author Tariq Ali paints a vivid picture of what is possible when ordinary people the world over do exactly that.<p>
Written as a memoir of his formative years as a radical activist, <i>Street Fighting Years</i> could almost be read just for the sheer whirlwind of events and encounters that are covered within it. Born in an area of India, now a part of Pakistan, Ali organized student demonstrations against the military dictatorship there before being banned from student politics, and with a seemingly pending arrest, fleeing the country to Britain. <p>
While playing a key role in the development of the student anti-Vietnam War movement, Ali crossed paths with Malcolm X, a supportive Marlon Brando and Henry Kissinger, with whom he debated the war on live television aired in Britain and the U.S. Ali then spent two months traveling Vietnam as an investigator for the International War Crimes Tribunal that would later publicize the atrocities of the war and gain significant international attention. <p>
In the wake of the Pentagon's recent admission of using the chemical weapon white phosphorous in Falluja, Ali's notes of his time there could just as well be written about Iraq. They include the systematic and deliberate bombing of schools and hospitals, the destruction of entire villages, incendiary bombs and torture.<p>
They also include a widespread resistance, fueled by the brutality of war, that refused to surrender its right to self-determination. One National Liberation Front (NLF) member remarked after the war was characterized as an intervention in the civil war there, "Many of you might believe that, but it is not true. The war is one between the people and the United States, who have created and funded a puppet administration."<p>
Iraq again comes to mind when considering the repeated failure of the Iraqi elections in making life bearable for the people there, and the role the U.S. is playing in setting sections of the population against one another.<p>
In Ali's retelling of the events around the world that occurred after his return from Vietnam, <i>Street Fighting Years </i>brings to light the most important lesson the decade of the 1960s can offer activists today. Our side has the ability to not only fight back, but also to win.<p>
Moving between Britain, the U.S., Pakistan and Paris, Ali evokes the fact that there are times when the strongest of movements rise inside the belly of the beast, when soldiers return to speak out, when militant demonstrations called in the name of solidarity with those resisting occupation spark involvement from even larger numbers, when students and workers shut down whole countries, when demonstrators out-number and out-organize the police, and when a small poor nation can defeat even the most powerful military in the world.<p>
But as inspiring as the movement was, it was not without its weaknesses. Many of which are highlighted, surprisingly, in an interview with a very radical John Lennon. Lennon speaks to the aspects of the movement that alienated people of working-class backgrounds, largely absent from the movement, and the fact that for any fundamental transformation of society, workers would have to organize.<p>
In that respect, <i>Street Fighting Years </i>lends much to activists today, offering the inspiration of vibrant movements around the world and also insight into what was lacking, so that next time we can win what so many of the sixties boldly fought for--not only an end to the war, but also a better world.<p>

