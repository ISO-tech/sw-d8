<font face="Times New Roman, Times, serif" size="4">Transit workers push for "no" vote on new contract</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Will transit deal pass?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jen Roesch</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 20, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Discontent about givebacks that mar gains won by a three-day strike of transit workers has created doubt about whether an upcoming vote will ratify the new contract.<p>
The strike by Transport Workers Union (TWU) Local 100 crippled New York, and the union was able to fight back a two-tier pension system for new hires and win some gains around disciplinary issues and maternity leave.<p>
But the new contract, backed by Local 100 President Roger Toussaint, also contains a clause that would require workers to contribute toward health care costs for the first time--a significant giveback that sets a precedent for city unions. Already, Bloomberg is calling for health care contributions from other city workers, and while contributions begin at 1.5 percent of pay, they are set to scale up with increasing health care costs and could easily spiral out of control.<p>
This is why many union members are planning to vote against the deal. While many workers are proud to have gone on strike, there is a great deal of anger at the contract it produced. <p>
"We thought we were going on strike against givebacks, but it turns out that we went on strike to exchange one giveback for another," said Steve Downs of Transit Workers for a Just Contract (TWJC) at a meeting about the lessons of the strike organized by the International Socialist Organization, Solidarity and some Green Party members.<p>
A group of union dissidents and dissatisfied members, including members of the Local 100 Executive Board, has organized a "vote no" coalition. Their first organizing meeting drew more than 100 transit workers, and in addition to rejecting the contract, they aim to rebuild rank-and-file organization within the union.<p>
Toussaint has held borough meetings about the contract to convince workers to ratify the deal. About 400 people attended the Brooklyn meeting and grilled Toussaint about the contract--with many expressing doubts about the health care costs and low wage increases. "We didn't strike to give up more and get less, but that's what happened," Richard Watson, a station agent and union steward who said he would vote against the deal, told the <i>New York Times.<p>
</i>At the Bronx meeting of 200, Toussaint faced a hostile audience. "Members beat him up from all sides, left, right and center," said Ainsley Stewart, vice president of the car equipment department and a TWJC member. "I've never seen anything like it. By the time he asked for questions, 20 hands went up. Things went so badly [for Toussaint] that by the time of the Queens meeting the next night, all the questions were screened ahead of time."<p>
It's clear that there is mass sentiment against this contract and a desire to draw a line in the sand against givebacks. <p>
The voting deadline is January 20, and it's difficult to predict whether that sentiment is strong enough to actually defeat the contract. In the event of a "no" vote, TWJC is arguing for division-wide meetings and divisional- and local-wide solidarity committees to build pressure for a good contract and prepare for a possible resumption of the strike. <p>
If the contract is ratified, then it will be crucial to take the momentum and renewed networks established through this campaign to rebuild rank-and-file organization. <p>
Regardless of which way the vote goes, the fighting example and renewal of confidence that the strike provided have marked a significant step forward--and raised the stakes for what's possible and necessary inside the labor movement.<p>
<i>Hadas Thier contributed to this report</i>.<p>

