<font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The modern-day robber barons</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 20, 2006 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">JERRY JASINOWSKI, president of the Manufacturing Institute at the National Association of Manufacturers, warned U.S. workers last year, "From airline pilots to auto assembly workers, employees need to help reduce their costs." Economist Peter Morici, a Clinton-era trade negotiator, likewise pronounced, "Companies cannot provide gold-plated health care benefits and open-ended pension commitments."<p>
Yet employers have eagerly granted themselves "gold-plated" increases in compensation while terminating pension and health benefits for workers.<p>
As United for a Fair Economy reported in August 2005, "The ratio of average CEO pay (now $11.8 million) to worker pay (now $27,460) spiked up from 301-to-1 in 2003 to 431-to-1 in 2004. If the minimum wage had risen as fast as CEO pay since 1990, the lowest-paid workers in the U.S. would be earning $23.03 an hour today, not $5.15 an hour."<p>
Working-class incomes have fallen dramatically in the industrial Midwest over the last six years. Median family income fell by 18 percent in Michigan and 12.2 percent in Illinois between 1999 and 2004. Illinois lost 220,000 manufacturing jobs by 2004, and median income fell by $6,383 per household.<p>
Declaring bankruptcy (with a wink and a nod) has developed into a corporate trend, allowing multinationals to force U.S. workers into direct competition with low-wage labor around the globe. <p>
Delphi, the world's largest auto-parts manufacturer, demanded wage cuts of up to two-thirds when it entered Chapter 11 in October--while offering its executives cash and bonuses estimated at $500 million.<p>
United Airlines received cuts totaling $2.56 billion from its workers in each year of a six-year contract when it declared bankruptcy in 2002. Now, having reneged on employee pensions, United plans to offer 400 of its executives $115 million in company shares, on top of their already bloated salaries.<p>
Profitable companies have also been emboldened to demand drastic wage and benefit cuts from their workers to remain "competitive" in the global economy. IBM, for example, recently jumped on the pension-bashing bandwagon--not because it needed to, but just because it could.<p>
The arrogance displayed by today's employers rivals that of their "robber baron" predecessors more than a century ago--when cutthroat capitalists fought ruthlessly against every attempt by workers to raise safety standards and wages, or to form unions. <p>
Then, as now, the government aided and abetted in this project, whichever of the two main parties happened to be in power. As historian Matthew Josephson recalled of the late 19th century Senate, "the opposing party [the Democrats], the 'outs,' were like the Republicans, who were usually the 'ins,' also led by masters of business or corporation lawyers."<p>
Shipping owner Cornelius Vanderbilt famously declared on one occasion, "Law? What do I care about law? Hain't I got the power?"<p>
The early January Sago mine disaster highlighted a similar arrogance. In its wake, the mainstream media suddenly "discovered" that many mines still use century-old techniques, including underground explosives, while routinely flouting safety laws without repercussion. Despite major advances in technology in recent decades, many operators failed to update life-saving oxygen canisters and underground communications, which could have saved the 12 Sago miners' lives. <p>
When confronted with these facts, Bruce Watzman, vice president of safety, health and human resources at the National Mining Association, informed reporters that coal operators are not responsible for updating miners' safety equipment. "We're not in the self-rescuer manufacturing business," he quipped.<p>
Some things, of course, have changed in the last 100 years. Significantly, today's cutthroat capitalists are fighting ruthlessly to reverse all the gains that unions won in the class battles of the 1930s.<p>
With class inequality now comparable to the 1920s, it is worth noting that the massive union struggles of the 1930s finally succeeded in shifting the balance of forces between capital and labor. Corporations have been hell-bent on shifting the balance decisively back toward capital ever since. <p>
As Warren Buffett, the world's fourth-richest man, noted in his 2004 annual letter to Berkshire Hathaway shareholders, "If class warfare is being waged in America, my class is clearly winning." If history has any relevance, this won't remain true for much longer.<p>

