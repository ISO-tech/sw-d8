<p><a name="Executions"></a><font face="Times New Roman, Times, serif" size="4"><b>End executions</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Alison McKenna</b></font><p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES--A multiracial crowd of about 55 people came out to Bethel AME Church on January 7 to a "Live from Death Row" event featuring Kenneth Clair, who spoke via speakerphone from San Quentin's death row.<p>
The event was organized by the Campaign to End the Death Penalty and Clair's private investigator C.J. Ford. It was attended by several of Kenneth's family members; Bonnie Williams, the former wife of the late Stan Tookie Williams, who was executed in December; and Doris McClain-Russell, the mother of California death row inmate Herbert McClain.<p>
McClain's mother spoke about how she believed "in the American way" and how now her innocent son "is locked up in a concrete grave."<p>
Inmates' family members took the opportunity to network with one another. Representatives from Community Call to Action, Death Penalty Focus, the International Socialist Organization and the Nation of Islam were present. Family members and activist groups in California will continue to put pressure on this racist system as the moratorium bill is voted on.<p>
