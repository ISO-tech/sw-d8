<font face="Times New Roman, Times, serif" size="4"><b>Stop the Nazis</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Mike Schwartz</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BOSTON--With one week's notice, 200 came out January 14 in the pouring rain to protest the Nazi group White Revolution and defend the Afro-American Meeting House and the legacy of Dr. Martin Luther King.<p>
This was White Revolution's "return" to Boston--after all eight of them got booted by 300 counterprotesters last May.<p>
The loud, confident multiracial crowd arrived early, and this show of strength scared off the Nazis and kept them from even showing up. An hour and a half after they had planned, 10 or so Nazis appeared at the State House a couple blocks away. <p>
The crowd quickly mobilized to find them and shut them down, but the racists were promptly given a ride back home to Cambridge by the friendly Boston Police Department (since when do cops give people rides home?). <p>
A post on a local Nazi Web site lamented that the day was a "total defeat." "It's definitely important that we don't ignore the Nazis," said student Rick O'Halloran, who spent the previous night making signs for the demonstration. "If we do that, they get to spread their message, and they look strong."<p>
After their humiliation, the Nazis planned to return two days later on Dr. King's official birthday, but about 60 counter-demonstrators organized in two days and returned once again to greet the Nazis. This time, they didn't show up at all.<p>
This was a major victory against the right in this city. Groups including the International Socialist Organization, the Progressive Labor Party, the International Action Center and several college and high school students from the region mobilized to pull off this event. <p>
Out of these two demonstrations, a statewide antiracist coalition was formed. A kick-off meeting has been called for January 29 at Northeastern University.<p>
<i>Contact <a href="mailto:no_nazis_boston@yahoo.com" target="_blank">no_nazis_boston@yahoo.com</a> for more information</i>.<p>
