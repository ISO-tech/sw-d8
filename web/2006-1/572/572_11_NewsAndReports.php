<p><a name="Abortion"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend abortion rights</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Amanda Maystead</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Planned Parenthood and about 30 supporters showed up outside Sen. Dianne Feinstein's office for a January 9 press conference to oppose the confirmation of Samuel Alito to the U.S. Supreme Court.<p>
Despite the fact that Feinstein has said she saw no need for a filibuster of Alito's nomination, all of the speakers praised her for her "courage" and "good work" defending our rights. But Tim Paulson, executive director of the San Francisco Labor Council, did point out the wide-ranging threat to workers' rights--from a woman's right to choose to environmental protection to safety on the job--that a nominee like Alito represents.<p>
This weekend, anti-choice bigots will return to San Francisco for a January 21 "Walk For Life." Last year, thousands of pro-choice activists turned out and matched the bigots person-for-persons, but this year, organizations such as Planned Parenthood, NARAL and NOW have announced that they don't plan to participate in any response. Nevertheless, pro-choice activists are organizing a demonstration.<p>
<i>The January 21 pro-choice protest begins at 11 a.m. at Pier 1. For more information, go to <a href="http://www.bacorr.org" target="_blank">www.bacorr.org</a></i>.<p>

