<font face="Times New Roman, Times, serif" size="4"><b>Time for labor to fight back</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ON JANUARY 8, I attended a protest in Detroit arranged by the Soldiers of Solidarity. Not only did I attend, but I had the privilege of riding up with one of the key organizers, Todd Jordan of Kokomo, Ind. I myself am not a Delphi or General Motors worker, but a fellow United Auto Worker (UAW) member who works at the Chrysler plant here in Kokomo. <p>
I'm sure that some readers, if not most, know about the wage and pension concessions proposed by the corporate CEOs and the wait-and-see attitude of the international UAW leadership. <p>
I am very new at all of this, but I always had a very radical and compassionate spirit, strongly believing that an injury to one is an injury to all. One thing I would like to point out is that this is more than UAW members frustrated with corporate greed and inept leadership of their top officials. What I witnessed at the demonstration was the beginnings of the entire working class getting fed up with our system. <p>
Upon returning to work, I was approached by many people curious over the event from someone who experienced it firsthand--people who were also supportive and expressed much anger and irritation at the corporations as well as the government. Many working-class people who I talk to seem to share the same thoughts and feelings that something must be done--and perhaps that something is a new revolution. <br>
<b>Chris Ryan,</b> Soldiers of Solidarity, from the Internet<p>
