<p><a name="Munich"></a><font face="Times New Roman, Times, serif" size="4"><b>An untold part of <i>Munich</b></font><p>

<font face="Times New Roman, Times, serif" size="3"></i>NEAR THE end of Geoff Bailey's review of Steven Spielberg's <i>Munich</i>, he writes: "When, late in <i>Munich</i>, Avner suggests that the Palestinian terrorists could have been arrested and tried against the backdrop of the United Nations, the unspoken question is never asked: Which court would ever try an Israeli assassin?" ("The conscience of a killer," January 13)<p>
This made me curious. The aftermath of the Munich strike is fairly well known in Norway, as it involved the assassination of Ahmed Bouchiki in the small Norwegian town of Lillehammer in 1974. However, some of the Mossad assassins were caught and later tried by a Norwegian court. Admittedly, they were given fairly short sentences, but they were convicted.<p>
From the rhetorical question in the above quote, it would seem that this incident is not featured in the movie?<p>
Ahmed Bouchiki's assassination was a case of mistaken identity. He was thought by Mossad to be another man, Ali Hassan Salameh (who was later assassinated by a car bomb). In reality, Bouchiki was a Moroccan with no links to any Palestinian organization.<br>
<b>Tor Fuglerud,</b> from the Internet<p>

