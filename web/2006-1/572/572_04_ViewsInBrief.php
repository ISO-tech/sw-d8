<p><a name="Ad"></a><font face="Times New Roman, Times, serif" size="4"><b>Turning art into ads</b></font><p>

<font face="Times New Roman, Times, serif" size="3">PRIOR TO the holiday season, Sony Corp., maker of Playstation, embarked on a marketing campaign which involved guerilla tactics, illegally advertising their new portable unit by recruiting graffiti artists. <p>
The work, consisting of figures interacting with the design of Sony's handheld unit, but without the Playstation logo, has been seen stenciled, painted and wheat-pasted directly on buildings in New York, San Francisco, Chicago, Philadelphia and Los Angeles. <p>
Graffiti is not a market. Graffiti in itself is an expression against the private ownership of public visual culture by the business community. It's a reclamation of a landscape by that which does not serve the market. Graffiti also supports a vibrant underground hip-hop culture, with grassroots accessibility. <p>
In a world dominated by the "bling bling" MTV spectacle, the underground's means of survival is in essence tied to the integrity of graffiti and its ability to forgo the art gallery. Any successful saturation into underground culture by such a mega-corporation would mark the beginning of the end for any faith an audience has in relation to such forms of expression.<p>
This paradigm represents the same saturation present in most of the media forms of our culture. One only needs to look as far as the news to observe the ambivalence in which a public views the truth of news coverage in its "incorporation." Once such forms of expression are permeated by capitalist integration, their very strength, in terms of either radical dissent or conservative existence as "public" domain (culture), are subverted into a private scheme for entering the market as objective things, and they thus lose their essence.<p>
These tactics cannot be tolerated. Thankfully, there has been a backlash against Sony in terms of graffiti artists vandalizing graffiti--defacing the defacement. Such endeavors as Sony's must be met with a determination of identity which shows itself in the equally energetic force to deface any such image, should you come across one. We must draw the line, physically, in the "act" of declaring that this is off limits.<br>
<b>Richard Martinez</b>, New York City<p>
