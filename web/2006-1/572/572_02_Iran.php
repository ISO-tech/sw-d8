<font face="Times New Roman, Times, serif" size="5"><b>Is Iran next on the U.S. hit list?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 20, 2006 | Pages 1 and 2</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>ALAN MAASS reports on the Bush administration's escalating rhetoric against Iran--and whether the country is next on the "war on terror" hit list.</b><p>

U.S. OFFICIALS are trying to line up supporters in their latest attempt to get United Nations (UN) sanctions imposed on Iran.<p>
The latest war threats began after Iran ended a two-year-old, self-imposed ban on research into uranium conversion while it negotiated with European countries over its nuclear program.<p>
The Iranian government says that its program is to produce nuclear power, not weapons. UN inspectors have yet to report any concrete evidence of a weapons program in Iran. America's spies agree. According to a <i>Washington Post</i> article last year, the administration's most recent National Intelligence Estimate on Iran concluded that the country is at least a decade away from obtaining "the key ingredient for a nuclear weapon."<p>
That's certainly not true of the U.S.--which has by far the largest nuclear arsenal in the world, and dominates the international arms trade. Yet U.S. officials complain that <i>Iran </i>is the threat to peace.<p>
Iran's move last week only begins research, not full-scale production, for power-plant reactor fuel. Nevertheless, George Bush and his administration are organizing to get members of the UN Security Council to take action against Iran--and they now have Germany's new conservative Chancellor Angela Merkel in their corner.<p>
U.S. officials were meeting with diplomats from other permanent members of the UN Security Council--Britain, China, France and Russia--in the hopes of lining up a vote this week. In particular, the U.S. is leaning on Russia and China to take a harder line on Iran--while implying, as George Bush has repeatedly, that military action is an open possibility.<p>
There was a heated confrontation over Iran's nuclear program last November, which ended in an agreement for further talks between Iran and European Union (EU) countries. Those negotiations didn't provide the surrender the U.S. demands, so Washington is stepping up its rhetoric again, this time with the support of the EU.<p>
The Bush administration's Republican allies in Congress followed the White House line in issuing dire threats.<p>
But don't expect the "opposition" Democrats to show much opposition. The Democrats' idea of a foreign policy alternative is to quibble over the details of the U.S. occupation of Iraq--and to out-Republican the Republicans in heaping abuse on Iran. Last Sunday, Democratic Sen. Evan Bayh (D-Ind.) matched the administration's tough rhetoric, insisting on CNN that an air strike "would dramatically delay the development" of an Iranian nuclear program.<p>
But it's Iran that has legitimate reasons for fear--not the U.S. It is surrounded by nuclear powers (Russia to the north, Israel to the west, India, Pakistan and China to the east) and bordered by two countries occupied by the U.S. imperialism.<p>
What's more, the U.S. is known to be developing a new generation of "usable" nuclear weapons--low-yield bombs known as "bunker busters" that are designed to penetrate deep enough to destroy underground targets. Last August, Pat Buchanan's <i>American Conservative </i>magazine printed a column by a retired CIA analyst revealing that the Pentagon had plans--ordered by Vice President Dick Cheney--for a retaliatory attack, possibly involving bunker-busters, in the event of "another 9-11-style attack."<p>
If Iran has avoided a U.S. attack so far, one major reason is the success of the anti-occupation resistance in neighboring Iraq--which has tied down U.S. forces. Indeed, Washington has been forced to abandon its plans for a "new" Iraq governed by U.S.-sponsored stooges--and now collaborates with political and religious leaders among Iraq's Shia majority who are closely connected with Iran.<p>
Nevertheless, despite this alliance of convenience, the hawks in Washington--not to mention the right-wing Israeli government--want Iran to pay a price for defying their authority. The threat of war on Iran grows greater with every new confrontation.<p>

