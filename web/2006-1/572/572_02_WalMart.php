<font face="Times New Roman, Times, serif" size="4">Maryland's new health care law aimed at the retail giant</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Making Wal-Mart pay</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 20, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>JOSH BRAND reports on the victory over Wal-Mart in Maryland.</b><p>

THE RETAIL Goliath Wal-Mart suffered one of its biggest defeats yet in the political arena when the Maryland General Assembly passed an affordable health care law last week. <p>
Lawmakers voted to override Republican Gov. Robert Ehrlich's veto of the legislation, called the Fair Share Health Care Fund Act, which requires companies employing over 10,000 workers in Maryland to either devote 8 percent of their payroll to providing employee health care, or pay the difference to a new fund that would help support the Maryland Medical Assistance Program.<p>
"This bill doesn't just affect one company," explained David Dunphy, a lobbyist for United Food and Commercial Workers (UFCW) Local 27, which represents 26,000 members in Maryland and nearby states. "But only Wal-Mart [meets the criteria]." <p>
In 12 of the 13 states where data is available, Wal-Mart leads all other employers in the number of workers on public assistance for their health care. But even achieving this first step in holding Wal-Mart accountable for its employees' health care was no small feat.<p>
According to the <i>Washington Post, </i>the campaign for the legislation grew out of a partnership between the UFCW and the Maryland Health Care for All Coalition. The two were later joined by Service Employees International Union and other unions in what Dunphy describes as "a fantastic coalition between labor, religious groups and progressive groups." <p>
The bill passed early last year, but Ehrlich vetoed it after the legislative session ended. So the coalition promoting the law geared up for a veto override battle. "The Chamber of Commerce and Wal-Mart spent a lot of money, hired a lot of lobbyists and made a full-court press to sustain the governor's veto," Dunphy said.<p>
In response, labor stepped up its remarkably unified lobbying effort, to make sure that the Democratic-controlled General Assembly made good on its promises. According to the <i>Post, </i>waffling Democratic legislators got calls from several unions, which tipped the balance--and Marylanders caught a glimpse of an all-too-rare spectacle: Democratic Party discipline triumphing over a Bush clone of a governor.<p>
The fight isn't over. Ehrlich has announced plans to file suit against the law, and Wal-Mart is notorious for fighting tooth-and-nail against any legal restrictions on its business practices--the company has already announced its own separate plans for a lawsuit. There are rumors that the company might even try to reorganize itself to get under the 10,000-worker threshold. <p>
As it stands, the Maryland law represents "a step in the process toward making sure everyone has access to affordable health care," Dunphy said in an interview. "The health care problem is not going away, and when the biggest employer in the United States fails to pay its fair share for its workers, it's up to us to take action." <p>
"Fair share" legislation may soon be taken up in other states--as many as 30, by union estimates. This is an important first step at Wal-Mart, but a much wider mobilization is going to be required to force the company to cave, and to win the goal of affordable health care for all.<p>

