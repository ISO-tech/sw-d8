<font face="Times New Roman, Times, serif" size="5"><b>Beware wolves in Democrat clothing</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 20, 2006 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">NOAM CHOMSKY recently made an incisive comment about the peculiar political situation we find ourselves in. <p>
When asked by Alternet's Geov Parrish, "Is George Bush in political trouble?" Chomsky responded: "George Bush would be in severe political trouble if there were an opposition political party in the country. Just about every day, they're shooting themselves in the foot. The striking fact about contemporary American politics is that the Democrats are making almost no gain from this. The only gain that they're getting is that the Republicans are losing support.<p>
"Now, again, an opposition party would be making hay, but the Democrats are so close in policy to the Republicans that they can't do anything about it. When they try to say something about Iraq, George Bush turns back to them, or Karl Rove turns back to them, and says, 'How can you criticize it? You all voted for it.' And, yeah, they're basically correct."<p>
Chomsky has hit on the key reason why--in spite of a swirl of problems that continue to trip up the Bush administration and the Republican Party, from Iraq to a host of political and financial scandals at home--they're still on their feet.<p>
That doesn't mean that there aren't any Democrats criticizing Bush. But the parameters of the argument are extremely narrow, hinging on the idea that the occupation of Iraq is being "mishandled."<p>
The semi-oppositional stance of the Democrats to Bush takes as its starting point a common agreement between the two parties that the U.S. should be able to dominate the world. The worry is that Bush, despite the rhetoric, isn't doing a very good job.<p>
Former Democratic President Jimmy Carter is one of those critics. He wrote a piece in mid-November expressing his growing concern over "a host of radical government policies that now threaten many basic principles espoused by all previous administrations, Democratic and Republican."<p>
What really bothers Carter about this administration is that "Regardless of the costs, there are determined efforts by top U.S. leaders to exert American imperial dominance throughout the world."<p>
Carter may come across today as a sort of benign and liberal old sage, but these statements are in flat contradiction to his practice as the 39th president from 1977-1981. When asked if the U.S. should rebuild Vietnam, a country it destroyed at a cost of three million Vietnamese lives and 58,000 U.S., his glib response was, "Well, the destruction was mutual."<p>
The Shah Pahlevi regime, which had come to power in Iran in a CIA-sponsored coup in 1953, was renowned for the murderous brutality of its secret police. Carter visited the Shah in 1979 and praised him for his "progressive administration" at a time when the Shah's military was gunning down thousands of unarmed demonstrators.<p>
In response to the Shah's fall in the 1979 revolution, Carter, in his 1980 State of the Union address, set forth his Carter Doctrine: "Let our position be absolutely clear: An attempt by any outside force to gain control of the Persian Gulf region will be regarded as an assault on the vital interests of the United States of America, and such an assault will be repelled by any means necessary, including military force."<p>
In Nicaragua, after the fall of the U.S.-backed Somoza dynasty, Carter began bringing together the remnants of Somoza's old national guard that would later be welded by the Reagan administration into counterrevolutionary paramilitary death squads, the <i>contras.</i> The Carter administration also provided arms to the El Salvadoran Junta at a time when it was murdering hundreds of trade unionists and peasant activists in a reign of terror.<p>
We can now more easily interpret Carter's meaning. It is not that he is bothered by the exertion of "American Imperial dominance throughout the world" so much as the fact that the current administration is accomplishing it at too high a cost.<p>
The antiwar movement does not need such false friends.<p>

