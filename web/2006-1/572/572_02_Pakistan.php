<font face="Times New Roman, Times, serif" size="4">U.S. air strike in Pakistan misses its target</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Only our family members died"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 20, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">A U.S. air strike in Pakistan killed 18 people, none of them remotely connected to "terrorism"--and sparked furious protests across the country.<p>
According to U.S. press reports, the January 13 attack was launched by the CIA, using unmanned Predator drone planes that fired missiles at three houses on the outskirts of the village of Damadola, across the border from Afghanistan. But some international media sources quoted reports suggesting the involvement of U.S. personnel on the ground--something that the Pakistani government has forbidden.<p>
The target was supposed to be Ayman al-Zawahiri, an al-Qaeda leader described as "Osama bin Laden's Number 2" by the media. But according to local officials, none of the victims of the air strike had any connection to al-Qaeda--at least a third were women and children.<p>
"Only our family members died in the attack," Shah Zaman, whose three children were killed, told Britain's <i>Observer </i>newspaper. "They dropped bombs from planes, and we were in no position to stop them...or to tell them we are innocent. I don't know al-Zawahiri. He was not at my home. No foreigner was at my home when the planes came and dropped bombs."<p>
One week earlier, another U.S. air assault killed eight people in a mountainous region 300 miles south of Damadola.<p>
Pakistanis took to the streets to demonstrate against the latest attack. <p>
In Khaar, the administrative center for the autonomous tribal region Damadola is a part of, nearly 10,000 people turned out to a protest against both the U.S. and the Pakistani government of Gen. Pervez Musharraf. Afterward, the headquarters of an aid organization funded by the U.S. Agency for International Development was set on fire.<p>
In Karachi, the country's biggest city, some 10,000 people gathered at a demonstration calling for Musharraf to step down. Other protests took place in the capital of Islamabad and other cities. Even Musharraf's government--a newfound ally of the U.S. in its "war on terror"--had to publicly denounce the attack as unilateral and unauthorized.<p>
But in Washington, U.S. officials were still defending the deadly assault. "We have to do what we think is necessary to take out al-Qaeda, particularly the top operatives," Sen. John McCain (R-Ariz.) declared on CBS's <i>Face the Nation</i>. His Democratic colleague, Sen. Evan Bayh (D-Ind.), echoed the defense of the White House on CNN: "It's a regrettable situation, but what else are we supposed to do?" <p>
The air strikes in Pakistan come as the U.S. is apparently escalating its air war in Iraq--with the same deadly results. On January 2, a U.S. warplane bombed a house in northern Iraq, killing at least eight people, including two children. Like the botched air strike in Pakistan, U.S. forces apparently targeted the house using a surveillance drone plane that--according to military officials--recorded three men planting a roadside bomb and then running into the house.<p>
According to press reports, U.S. plans to draw down troop strength also involve an escalation of the air war--guaranteeing more horrors like the one that struck down 18 people in Pakistan last week.<p>

