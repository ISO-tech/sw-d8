<font face="Times New Roman, Times, serif" size="5"><b>Protests at UNC against anti-Muslim cartoons</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Julie Southerland</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 3, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">GREENSBORO, N.C.--Protesters occupied the offices of the student newspaper at the University of North Carolina (UNC) at Chapel Hill February 20 following the paper's publication of racist anti-Muslim cartoons.<p>
The cartoons were first printed February 9 in the <i>Daily Tar Heel (DTH), </i>the main campus paper. When the <i>DTH </i>ignored calls to apologize from the campus Muslim Student Association (MSA), more than a dozen students at UNC, including MSA members, sat in at the <i>DTH </i>office.<p>
The protest organizer, Salma Mirza, said they had more than 100 signatures on a petition stating they would not pick up another copy of the student newspaper until an apology was issued. However, the <i>DTH </i>has flatly refused to apologize, claiming that the issue is a matter of free speech.<p>
But as an MSA letter to the newspaper pointed out, the cartoons were designed to insult and provoke Muslims. "We also ask that the <i>DTH </i>exercise a greater measure of responsibility by not repeating such bigoted actions," the MSA stated, adding that "any depiction of the Prophet is a known violation of Islamic theology, understood and respected throughout the world. To intentionally tag the disgraceful depiction with an implication of terrorism is unacceptable."<p>
In Greensboro, the <i>Rhinoceros Times</i>, a conservative weekly, reprinted several of the cartoons in their February 16 issue and also have refused to apologize for doing so. The main newspaper in Greensboro, the <i>News and Record, </i>has written a series of articles on the cartoon controversy and received a flurry of letters and calls in response.<p>
Many letters express disgust over the cartoons, but a significant minority praised the <i>Rhinoceros Times </i>for "having the courage" to print them and demanded the <i>News and Record </i>follow suit. Meanwhile, the Greensboro Muslim community has responded, with the Islamic Center of the Triad calling for weekly protests outside of the <i>Rhinoceros Times </i>office until the paper apologizes.<p>
The newspaper's editor has refused--and has promised to print even more of the cartoons. Others protesting the racist cartoons include the UNC-Greensboro chapter of the Campus Antiwar Network, the International Socialist Organization and various community activists. <p>
These protests need to be part of a national response to these cartoons, which have contributed to an atmosphere of racism against Arabs and Muslims. This is not a free speech issue--it is about hate speech used to demonize Muslims.<p>
