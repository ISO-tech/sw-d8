<font face="Times New Roman, Times, serif" size="4">Veterans plan demonstrations along West and Gulf Coasts</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>On the march against occupation</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 3, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">VETERANS AND military family members are planning two marches this month to mark the third anniversary of the U.S. invasion of Iraq.<p>
On the West Coast, Latino war resisters and family members are organizing a three-week, 241-mile march from Tijuana, Mexico, to San Francisco from March 12 to March 27. And along the hurricane-ravaged Gulf Coast, several veterans' organizations will march from Mobile, Ala., to New Orleans from March 15 to March 19.<p>
For Pablo Paredes, a war resister and central organizer of the West Coast march, it's a critical time to build a strong current of opposition among Latinos to the U.S. occupation of Iraq.<p>
"In the first months of the invasion, 20 percent of the casualties in Iraq were Latinos," Pablo said in an interview. "This number in no way matches our percentage of the overall population, which is about 15 percent, much less our proportion of the military ranks, which is 11 to 12 percent. The numbers add up to a systemic railroading of my people to the front lines of a war for profit."<p>
Pablo, a third class petty officer, was sentenced to two months restriction and three months hard labor for his refusal to board a ship bound for Iraq in December 2004.<p>
"I think that the serious racism manifested in immigrant rights violations, the escalation of funding for border patrols and the upcoming legislation that would make it a felony to aid an undocumented immigrant are all umbilically attached to the war," he said. "It is the state of fear that the Bush administration promotes that allows these racist policies to be openly considered. It is the aggression being unleashed on Iraq--and the people of Iraq's understandable resistance to this aggression--that leads to budget cuts in many programs that are vital to the Latino community."<p>
Other Latino antiwar activists involved in the march are Fernando Suarez del Solar, the father of one of the first U.S. soldiers to die in Iraq; Camilo Mej&iacute;a, a war resister who was jailed for nine months for refusing combat; and conscientious objector Aidan Delgado. The marchers will stop at historic and symbolic points along the way and are calling on local organizers to complement their efforts.<p>
Along the Gulf Coast, members of Iraq Veterans Against the War (IVAW), Veterans For Peace, Vietnam Veterans Against the War, Military Families Speak Out and Gold Star Families for Peace will march with Hurricane Katrina survivors and civil rights leaders.<p>
"There are many similarities between how the people of Iraq and hurricane survivors along the Gulf Coast are being treated," said IVAW member Kelly Dougherty. "In Iraq, we're building cities overnight for the purpose of running a military occupation, but ordinary Iraqis are going without food, water and electricity. <p>
"The same corporations that get no-bid contracts in Iraq are getting no-bid contracts on the Gulf Coast, but not to rebuild the homes of working people. These corporations, instead of hiring local workers at fair wages, are importing workers, paying them below scale and rebuilding higher rent housing than was there before, so now people who used to live there can't afford to come back."<p>
<i>For more information about how you can support--financially or otherwise--both of these antiwar marches, go to <a href="http://www.guerreroazteca.org" target="_blank">www.guerreroazteca.org</a> and <a href="http://www.vetgulfmarch.org" target="_blank">www.vetgulfmarch.org</a></i>.<p>

