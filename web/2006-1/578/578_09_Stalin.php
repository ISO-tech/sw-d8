<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The real crimes of Stalin's Russia</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 3, 2006 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">FEBRUARY 25 marks the 50th anniversary of Soviet leader Nikita Krushchev's "secret speech" to the Communist Party.<p>
Krushchev exposed the horrible crimes of Stalin, who had died in 1953. Describing Stalin as "violent," "capricious" and "despotic," he cited Lenin's suppressed 1922 testament asking for the removal of Stalin from his position as general secretary of the party.<p>
Krushchev revealed that Stalin's great purges in the late 1930s had almost wiped out the political leadership of the party that had had any connection to the 1917 revolution itself, through the use of coerced confessions and summary executions.<p>
Stalin had actually done more than that. He had sent millions to forced labor camps and had forcibly collectivized the peasantry at enormous human cost. Workers were deprived of all power and of any social or economic rights and expected to out-produce each other for the "glory" of comrade Stalin, who was virtually deified.<p>
Hardcore Stalinists saw Krushchev's speech as a betrayal; others left their respective Communist parties in disgust and disillusion; and Western leaders used it to "prove" that socialism doesn't work. But the significance of Krushchev's speech lay elsewhere.<p>
In Russia, it represented a political thaw that had developed since Stalin's death. As author Tony Cliff writes, "Stalin's method of 'solving' any problem was to increase pressure and terrorism. However, the growth of the economy and society under Stalin himself, made this method more and more anachronistic and self-defeating."<p>
According to Cliff, the "arbitrariness and excesses" of terror impeded economic rationality and led not to increased output, but to the demoralization and paralysis of Russian workers. At a certain stage of economic development, further progress was not possible without some relaxation of terror, and economic incentives such as higher wages and better social welfare.<p>
The ruling class--the high state and party bureaucrats themselves--wished to enjoy their privileges, and so they too wished for some degree of relaxation of fear and suspicion.<p>
Stalin's regime had had nothing to do with socialism. His rule was consolidated--as his liquidation of the old party leadership shows--on the ruins of everything the Bolshevik Party and Russian workers had fought for in 1917.<p>
The conditions of his rise were tragically simple: Russia's terrible backwardness, exacerbated by civil war, isolation and encirclement. Stalin's bureaucracy rose on the disintegration of the working class, and therefore of the soviet (council) democracy that the revolution had established.<p>
Socialism cannot be built in conditions of scarcity. Some distorted socialist rhetoric remained to justify itself, but the real content of Stalin's regime, as it emerged from the mid to late 1920s, was that of a growing state power, elevated above society, increasingly serving its own interests.<p>
What were those interests? They were those of other ruling classes in a system of competing world capitalism: national economic development, not simply to build up a surplus to enrich the lives of the minority at the top, but accumulation of capital--in this case, state accumulation of capital, since now there were no private owners in Russia--in order to plough the surplus back into heavy industry.<p>
In the 1930s, Stalin embarked on a plan of hothouse industrial development to "catch up and overtake" the West in 10 years, in order to be able to build in Russia a formidable industrial military machine capable of holding its own against the West, particularly the United States.<p>
This insanely rapid pace of industrial development required the super-exploitation of workers and peasants, and in conditions of backwardness, methods of extreme state coercion to dragoon the population into line. As Russia industrialized, these methods were now a hindrance.<p>
But whenever a ruling class relaxes its totalitarian grip, there is always a danger that the relaxation will provoke a political awakening that threatens to go beyond the bureaucracy's own limited aims. Such is what happened in 1956 in Hungary. That will be the topic of the next column.<p>

