<font face="Times New Roman, Times, serif" size="4">Film takes on the "military-industrial complex"</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why the U.S. goes to war</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Jonah Birch</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 3, 2006 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Why We Fight,</b></i> written and directed by Eugene Jarecki.</font><p>

<font face="Times New Roman, Times, serif" size="3">IT'S A testament to the level of anger that exists about the Iraq war that antiwar films such as the wildly popular <i>Fahrenheit 9/11</i> and Eugene Jarecki's recently released <i>Why We Fight</i> have been so successful in recent years.<p>
Winner of the Grand Jury Prize at the 2005 Sundance Film Festival, <i>Why We Fight</i> is an insightful documentary that explores the relationship between the "military-industrial complex" and the invasion of Iraq, as well as Americans' changing attitudes toward the occupation.<p>
Featuring interviews with commentators ranging the political spectrum, from left-wing author Chalmers Johnson to neoconservative hack Richard Perle, the film details the history of the U.S.'s interventions since the Second World War and the events leading up to the Iraq war.<p>
Jarecki structures <i>Why We Fight</i> around former President Dwight Eisenhower's famous 1961 "farewell speech" in which he warned against the growing influence of the "military-industrial complex" and the dangers of developing a "permanent armaments industry." The film suggests that recognizing the influence exerted by this "complex"--which includes members of Congress, corporate leaders, military officials and influential, hawkish think tanks--is key to understanding the vast quantities that the U.S. government expends on the military each year and the eagerness of U.S. politicians for wars and interventions abroad.<p>
Easily debunking the administration's justifications for the invasion of Iraq, Jarecki demonstrates that key advisors to Bush--grouped around the Project for a New American Century, a right-wing think tank headed by arch-neocon William Kristol--developed plans for the Iraq war as early as 1992.<p>
<i>Why We Fight</i> does a tremendous job putting the Iraq war in historical context--its discussion of the U.S.'s long-term relationship with Saddam Hussein is especially useful.<p>
The film makes clear that Iraq is merely the latest in a series of imperial adventures that the U.S. government has falsified its way into. "We have been lied to in every military escapade in the last 50 or 60 years, without exception," says Charles Lewis of the Center for Public Integrity.<p>
September 11 provided a useful excuse for supporters of the "military-industrial complex" to gain support for their project of remaking the face of the Middle East, in the interest of "economic colonialism," as Lewis calls it.<p>
While a few have grown wealthy off the war, thousands have been victimized--from the Iraqi civilians killed by U.S. air strikes in the first hours of the war to the disillusioned father of a 9/11 victim who supported the war until he discovered that Saddam Hussein had nothing to do with the World Trade Center attacks. <p>
While generally very strong, <i>Why We Fight</i> has several important weaknesses.<p>
First, the diversity of commentators that Jarecki employs leads to a degree of political incoherence in the film. Is Iraq a "distraction" from a defensible war on terror or a natural progression flowing out of the basic logic of U.S. foreign policy after 9/11? Do the neocons alone bear primary responsibility for the invasion or was it the basic structure of the American political-economic system that pushed the country toward the war? At times, <i>Why We Fight</i> offers conflicting answers to these questions while glossing over the contradictions between different commentators' arguments.<p>
Second, the overarching "Eisenhower-as-antiwar-visionary" theme gets very tired, very quickly. Eisenhower was nothing of the sort, and was as responsible as anyone for brutal American interventions in the Congo, Guatemala, Iran, Lebanon and elsewhere during the 1950s.<p>
Furthermore, the glorification of Ike reflects a larger flaw in the film's overall analysis: <i>Why We Fight</i> suggests that American imperialism is both a relatively recent phenomenon (which developed during the Cold War) and some sort of distortion of American capitalism and "democracy."<p>
In fact, the competition between nation-states for markets and resources that breeds wars like those in Iraq and Vietnam is merely an extension of the competition that drives capitalist society; the U.S. has a long and bloody history of foreign interventions dating back to the 19th century. <p>
Whatever its weaknesses, however, <i>Why We Fight</i> is an excellent film, which raises key questions about the structural roots of the Iraq war. <p>

