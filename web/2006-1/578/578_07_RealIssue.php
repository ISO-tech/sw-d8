<font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The real issue is anti-Arab racism</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 3, 2006 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">THOSE WHO worry that the world's Arab and Muslim populations pose a threat to free speech in Western democracies need not fear. The First Amendment remains intact--particularly, it seems, when it comes to the "right" to inflict racial slurs. <p>
Indeed, the last few weeks have witnessed a spate of pundits and politicians exercising their right to freely engage in racist demagoguery against Arabs and Muslims without repercussion. <p>
Celebrity hatemonger Ann Coulter did not disappoint the rabid crowd at the annual gathering of the Conservative Political Action Conference (CPAC) in Washington, D.C. last month. <p>
The highlight of Coulter's address, sandwiched between speeches by Dick Cheney, Senate Majority Leader Bill Frist and Newt Gingrich, was: "I think our motto should be, post-9-11, 'raghead talks tough, raghead faces consequences.'" Journalist Max Blumenthal remarked, "This declaration prompted a boisterous ovation" from the overflow crowd.<p>
Fifteen minutes later, Blumenthal asked Frist his opinion on the "raghead" characterization. Frist responded, "I wasn't there so I better not comment." No major newspaper reported on Coulter's racial epithet to the more than 1,000 Republican Party stalwarts.<p>
The "raghead" comment is consistent with an article Coulter posted on her Web site, which reads in part, "Jihad monkey talks tough; jihad monkey takes the consequences. "Sorry, I realize that's offensive. How about 'camel jockey'? What? Now what'd I say? Boy, you tent merchants sure are touchy."<p>
Last week, the level of xenophobia surged on Capitol Hill when Democrats Hillary Clinton and Chuck Schumer, along with Frist and House Speaker Dennis Hastert, whipped themselves into a (bipartisan) frenzy to block Dubai Ports World from replacing a British company in running six U.S. ports. As the <i>Washington Post </i>reported, "The lawmakers said they feared that national security might be compromised by letting a Middle Eastern firm manage key U.S. ports." <p>
Raising the level of melodrama, Clinton argued that the port management deal would "turn over our sovereignty to another country." New York Republican Rep. Vito Fossella compared the port deal to an "announcement that Dubai was to take over security at our airports." <p>
Schumer told reporters, "How can we turn over one of the most vital areas in our nation to a country with a significant nexus of involvement with terrorists?" Although Schumer argued in a February 22 <i>USA Today </i>op-ed article that Congressional opposition "has nothing to do with the fact that the United Arab Emirates...is an Arab nation," the Democrats' media attack dogs were already cut loose.<p>
That same day, <i>New York Times </i>columnist Maureen Dowd called for "corporate racial profiling," quoting Jan Gadiel of 9/11 Families for a Secure America: "Our borders are wide open. We don't know who's in our country right now, not a clue. And now they're giving away our ports."<p>
Even Dowd admitted that election-year politics played a role in the Congressional mutiny. "Lawmakers, many up for re-election, have learned well from Karl Rove," she wrote. "Playing the terror card works." <p>
<i>Wall Street Journal </i>columnist Dan Henninger argued, "It gave the Democrats an opportunity to get to the right of the president on a terror issue, and attack him for being soft on terror."<p>
It is now acceptable--indeed, commonplace--to racially stereotype and denigrate Arabs and Muslims. And there is no outcry against the curtailment of their civil liberties and rights.<p>
A December 2004 Cornell University opinion poll showed 44 percent of Americans approved curtailing some civil liberties for all Muslim Americans--including registration with the federal government, close monitoring of mosques by law enforcement agencies, and racial profiling of citizens of Muslim or Middle Eastern heritage.<p>
Interestingly, a recent Gallup World Poll of predominantly Islamic countries showed that overwhelming majorities said they favored the right to freedom of speech in their own countries.<p>
As journalist Robert Koehler remarked, "Maybe we should be careful about making common cause with born-again free speech advocates who never showed any tolerance for it until it became a handy club for bashing Muslims." He added that in the current atmosphere, "It's okay to torture them because they've already been dehumanized en masse. Anything could follow."<p>

