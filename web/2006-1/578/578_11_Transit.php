<font face="Times New Roman, Times, serif" size="4">NYC transit workers reject contract arbitration</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Transit struggle continues</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Dominic Renda</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 3, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Transport Workers Union (TWU) Local 100 rejected February 27 a proposal to arbitrate contract negotiations, the latest phase in a battle that included an illegal three-day strike in December.<p>
Union leaders called off that strike following a tentative agreement in which the Metropolitan Transportation Authority (MTA) withdrew demands for lower pensions for new hires and other concessions. Yet the proposed deal included other givebacks--most importantly a requirement that workers pay health care costs equivalent to 1.5 percent of their wages.<p>
After workers voted to reject the deal, the MTA, backed by Republican Gov. George Pataki, pushed an even worse deal, reinstating its demands for concessions on pensions for new hires.<p>
New York City Mayor Bloomberg plans to push for similar givebacks from other unions. In his State of the City address, he declared that unions have to accept cuts to health insurance and pensions, whose costs, he said, "have spiraled out of control."<p>
But despite Bloomberg painting a picture of a city with empty pockets, the <i>New York Daily News </i>reported the MTA surplus is growing, with $51 million more than the $1.04 billion originally reported during transit contract negotiations. The state budget also has a $2 billion surplus.<p>
Even United Federation of Teachers President Randi Weingarten, who let her members go years without a contract before obtaining a sellout deal last fall, said the mayor's comments are "a declaration of war." In regard to attacks on pensions and health benefits, she said, "When the MTA did it to the TWU workers, it provoked a strike."<p>
Weingarten, however, was one of many union leaders that pressured TWU President Roger Toussaint to end the strike before reaching a tentative agreement. And not one politician--including those endorsed by Local 100--supported transit workers' right to strike or opposed the state's fines for violating the anti-union Taylor Laws.<p>
Transit Workers for a Just Contract (TWJC), a rank-and-file group within TWU, argue that if the strike hadn't ended prematurely, the proposed contract offer wouldn't have contained concessions in health care. TWJC also points out that the 1980 transit strike, which lasted 11 days, won almost 21 percent in raises over two years, much better than what was offered in 2005--a 10.5 percent increase over three years.<p>
Since the 2005 contract offer was rejected, union officers and staffers have been criticizing the membership for rejecting the deal. Toussaint and his supporters seem determined to get the membership to vote on the same giveback contract they rejected.<p>
For their part, TWJC leaders are demanding an emergency mass meeting of Local 100 members to allow members the right to discuss and vote on different contract strategies. The Local 100 bylaws state that members at a mass membership meeting have even more power than Toussaint or the Executive Board, opening the way for the rank and file to take the initiative. <p>
As TWJC states, "A great way to begin to flex our muscles would be a massive labor protest at the MTA, so big it shuts down midtown. Community activists and students could join us. If properly organized, the MTA would be forced to wonder, 'There's such a huge turnout. What'll they do next...strike?' There's no reason why Toussaint can't turn-up the heat on the MTA!"<p>
