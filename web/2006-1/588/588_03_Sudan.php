<font face="Times New Roman, Times, serif" size="5"><b>Behind the "peace" deal for Darfur</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 12, 2006 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">A U.S.-backed peace deal over the Darfur region of Sudan is being promoted as the way to end three years of horrific slaughter, and has been welcomed by some liberals and progressives.<p>
But the negotiations are really aimed at opening the way for Washington's imperial power plays in Africa, under the guise of a "humanitarian" intervention.<p>
Since 2003, the government has responded to an insurgency in the country's westernmost province by setting Arabic-speaking militias, known as janjaweed, on a rampage of murder, rape and arson against villagers of the four language groups that form the insurgents' base. The scorched-earth war has killed an estimated 200,000 people, created 2 million refugees and caused an international outcry.<p>
A peace deal, brokered by U.S. Undersecretary of State Robert Zoellick, calls for disarming the combatants--starting with the janjaweed--a voice for Darfur in the central government, and a pledge of government and international aid.<p>
But the agreement is silent on how its terms are to be enforced. U.S. military muscle would have to be involved--in the form of supplies and logistics, if not troops themselves. Two U.S. corporations already perform military support for the African Union (AU) force and 10,000 United Nations (UN) troops currently in Sudan to enforce a separate peace deal in the southern part of the country.<p>
Many Darfuris themselves call for Western military intervention because they don't trust the government or the janjaweed to abide by any brokered peace. Large numbers of people in the U.S. agree--including many who are disgusted by the U.S. occupation of Iraq.<p>
But the feeling that "something must be done" about the horrors in Darfur won't turn the U.S. military from an instrument of imperialism in Iraq to a force for peace in Sudan.<p>
A closer look at the Save Darfur Coalition should raise doubts about just how "humanitarian" the U.S. intervention really is. Behind celebrities like actor George Clooney are a collection of Iraq hawks such as Rep. Tom Lantos (D-Calif.) and pro-Israel organizations dedicated to justifying Israel's occupation of Palestine to U.S. audiences.<p>
What the U.S. and Israel care about in Sudan isn't the suffering in Darfur, but vast oil reserves that are increasingly important to China's rising economy.<p>
The next step towards peace in Darfur, according to George W. Bush and UN Secretary General Kofi Annan, is to replace 7,000 AU peacekeepers already in Darfur with a UN-flagged force of 20,000, with NATO initiating the buildup. But even if the government allows NATO-UN forces in, there are many likely sources of friction once they get there--not least the fact that the janjaweed militia may refuse to disband.<p>
In these unsettled circumstances, Western forces entering Darfur would likely become fighters, not peacekeepers.<p>
The likely result is a replay of the U.S. intervention in Bosnia and Kosovo in the former Yugoslavia, which began with bombing raids and ended with a UN-NATO occupation, which institutionalized the very ethnic divisions that created the pretext for intervention in the first place. And in Iraq, U.S. intervention has actually intensified religious and ethnic divisions among Kurds, Sunnis and Shiites. The U.S. is playing a similar divide-and-conquer game in Sudan.<p>
Although the main Darfur rebel group, the Sudanese Liberation Movement, signed the peace deal, several smaller groups have not.<p>
Even if some Darfuris were to greet a NATO-UN force as liberators, they'd end up hating them as the imperial occupiers they would inevitably become. That's why military intervention is not the answer to the Darfur crisis.<p>
The UN recently announced that it was cutting back on its relief efforts for refugees in Darfur because of funding shortfalls. The starting point for genuine humanitarianism would be flooding Darfur with food and other forms of aid--not sending soldiers.<p>

