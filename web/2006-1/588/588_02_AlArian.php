<font face="Times New Roman, Times, serif" size="5"><b>Al-Arian's nightmare continues</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 12, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">U.S. DISTRICT Judge James Moody is doing everything in his power to prolong the suffering of former Unviersity of South Florida professor Sami Al-Arian.<p>
Last month, after 38 months unjustly imprisoned, Al-Arian reached a plea agreement with federal prosecutors. Al-Arian was arrested in February 2003, along with several codefendants, on charges that included racketeering, conspiracy and materially aiding "terrorists" by using an academic think tank, a Muslim school and a charity as a cover for raising money for the Palestinian Islamic Jihad (PIJ).<p>
After a six-month-long trial, a jury acquitted Al-Arian in December of eight of 17 charges against him, including the most serious ones. <p>
But the witch-hunt wasn't over. After the verdict, Al-Arian was kept in prison for months while the government considered whether to retry him.<p>
In order to end his ordeal and be reunited with his family, Al-Arian agreed last month to plead guilty to a single count of the most minor charge--conspiracy to "make or receive contributions of funds, goods or services to or for the benefit of" the PIJ. <p>
In exchange, prosecutors recommended that Al-Arian be given a sentence of 46 months--and then be deported. That would have left Al-Arian to serve a maximum of eight more months before being thrown out of the country.<p>
But the deal was too lenient for Moody, who instead sentenced Al-Arian to the maximum possible sentence on the charge. Al-Arian will now have to serve as much as 18 more months in prison.<p>
In a statement during the sentencing, Moody claimed that Al-Arian was guilty of terrorism, charging that he was "a leader of the PIJ" and had encouraged suicide bombings--a charge rejected by the jury. "Your children attend the finest universities this country has to offer while you raise money to blow up the children of others," Moody said. "You are indeed a master manipulator."<p>
As the Tampa Bay Coalition for Justice and Peace noted, "None of [Moody's] accusations, which were unequivocally rejected by the jury, appear anywhere in the actual plea agreement for which Dr. Al-Arian was being sentenced, or in the pre-sentencing report issued by a probation officer. He regurgitated long-disproven accusations by the prosecution and even rehashed a charge by a previously discredited FBI informant. During the course of his tirade, Moody resorted to name-calling and personal insults...<p>
"On a day when all sides expected a swift and final resolution to this painful chapter, the judge ensured Dr. Al-Arian's and his family's continued suffering and sparked outrage by friends, colleagues and supporters. Following the vindictive display, many experienced lawyers and legal experts said they had never seen a judge carry himself in such an unscrupulous manner."<p>

