<font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The grim consequences of "abstinence only"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 12, 2006 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">ONE OF the Christian Right's greatest ideological victories since the 1990s has been the dominance of federally funded "abstinence-until-marriage" programs now taught to millions of teenagers across the country.<p>
New evidence, however, suggests that these programs have contributed to soaring rates of unplanned pregnancies, out-of-wedlock births and--yes--abortions among women who are young or poor.<p>
Abstinence education was not an invention of the Bush administration, but was quietly tucked into Bill Clinton's 1996 welfare reform bill--dangling federal grants for abstinence-only programs to cash-starved states--funded with $97 million in 1999 and rising to nearly $170 million last year.<p>
Ironically, teenage contraceptive use had nearly doubled during the Reagan era, when comprehensive sex education--including contraception instruction--still remained the norm. By contrast, U.S. law now requires federally funded sexuality education to inform teenagers that "sexual activity outside the context of [monogamous and heterosexual] marriage is likely to have harmful psychological and physical effects."<p>
These programs do not let the facts stand in the way of traumatizing teenagers from developing a remotely healthy attitude toward their own sexuality.<p>
As the <i>Washington Post</i> reported in 2004, "Many American youngsters participating in federally funded abstinence-only programs have been taught [falsely] over the past three years that abortion can lead to sterility and suicide, that half the gay male teenagers in the United States have tested positive for the AIDS virus, and that touching a person's genitals 'can result in pregnancy.'"<p>
A congressional study in 2004 discovered that the curricula used by 69 educational organizations in 25 states taught adolescents that a 43-day-old fetus is a "thinking person" and that the HIV virus can be spread by sweat or tears.<p>
In addition, abstinence-only programs universally neglect to teach teenagers to <i>use</i> contraception, instead emphasizing false accusations about contraceptive <i>failure</i>. The congressional study found, for example, that many abstinence programs claimed condoms fail to prevent HIV transmission as often as 31 percent of the time, when the actual statistic is just 3 percent.<p>
Not surprisingly, abstinence promotion has not decreased teen sexual activity, but has led to an increase in unprotected sex. <p>
A recent Columbia University study found that 88 percent of teenage girls who take "virginity pledges" eventually have premarital sex--but are one-third less likely to use contraception when they do. At least two-thirds of American teenage females have had sex by the time they reach the age of 18, according to the Center for Disease Control. Over the course of any given year, nine in 10 teenagers who have heterosexual sex without contraceptives become pregnant.<p>
Nearly 80 percent of U.S. teen births take place outside of wedlock--overwhelmingly to low-income women--while one in four sexually active teens acquires a sexually transmitted disease. Federal funding for contraception has declined by 59 percent (in constant dollars) since 1980. As a result, women living in poverty are almost four times more likely to become pregnant unintentionally than women of even moderate means.<p>
According to reproductive rights researchers from the Alan Guttmacher Institute, the rate of unplanned pregnancy <i>rose</i> by nearly 30 percent for women living below the federal poverty line between 1994 and 2001--while it <i>fell </i>by 20 percent during the same time period for women in families earning more than $16,000 annually for a family of three.<p>
Among the poorest women, the proportion of unwanted pregnancies that resulted in live births increased by almost 50 percent between 1994 and 2001, while it declined for women in families whose income was at least twice the official poverty level.<p>
Unintended pregnancies led to almost even numbers of births and abortions. U.S. teen pregnancy rates are double those in England and Canada, and nine times more than the Netherlands and Japan. <p>
Research shows that when contraception is readily available, the rate of unplanned pregnancy <i>drops.</i> France offers free emergency contraception to teenagers, without requiring them to inform their parents, yet France has an abortion rate half that of the U.S. <p>
And for the record, no scientific evidence exists to show that consensual sex between teenagers is harmful in any respect.<p>

