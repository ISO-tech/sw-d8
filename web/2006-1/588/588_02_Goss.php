<font face="Times New Roman, Times, serif" size="4">CIA chief Porter Goss quits</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Rise and fall of a right-wing hack</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 12, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">"JUST ONE of those mysteries." That's what Porter Goss called his own sudden resignation on May 5 as director of the CIA.<p>
Goss, who spent just 19 months in the job as head spymaster, was surrounded in scandal as he left. <p>
Initial reports linked Goss's departure with allegations centering on Kyle "Dusty" Foggo, who Goss picked to be the number three man in agency. Foggo was exposed for handing out a $2.4 million contract to defense contractor and college buddy Brent Wilkes.<p>
Wilkes reportedly has ties to former Rep. Randy "Duke" Cunningham (R-Calif.), who was sentenced to more than eight years in prison after he pled guilty in November to collecting $2.4 million in yachts, homes and other bribes in exchange for help in getting government contracts. Wilkes is also believed to have supplied prostitutes for Cunningham's poker parties, which Foggo is reported to have attended.<p>
While there is no direct link yet between Goss and Cunningham, Goss's appointment of Foggo--a midlevel logistic officer at the time--didn't win him friends among CIA veterans.<p>
But the real scandal of Goss' short time at the CIA was his fealty to the Bush administration and its foreign policy agenda. When Bush appointed Goss to replace CIA Director George Tenet in 2004, the agency was in disarray over discoveries that it failed not only to detect the September 11 attacks, but to find "weapons of mass destruction" in Iraq.<p>
According to CIA veterans, Goss was completely unprepared for the job. Goss himself said during a public appearance in 2005, "I'm a little amazed at the workload."<p>
Soon after taking the position, Goss was demoted when John Negroponte was appointed to the new position of director of national intelligence in 2005, a cabinet-level position created to oversee all national intelligence agencies.<p>
Despite his lack of formal experience, however, Goss served the Bush administration well--as a leak plugger and hatchet man. As the <i>Washington Post's </i>David Ignatius wrote, "Though Goss long ago served as a CIA case officer, he arrived from Capitol Hill with a phalanx of conservative aides, soon dubbed the 'Gosslings,' who viewed the agency as a liberal, leak-prone opponent of conservative causes."<p>
For example, Goss forced out Mary McCarthy for allegedly leaking classified information about secret CIA prisons abroad where detainees were tortured.<p>
Fittingly, it looks like Goss's replacement will be Air Force Gen. Michael Hayden, Negroponte's top deputy--a former director of the National Security Agency and fierce supporter of Bush's NSA eavesdropping program.<p>

