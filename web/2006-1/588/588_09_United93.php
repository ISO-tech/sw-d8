<font face="Times New Roman, Times, serif" size="4">Paul Greengrass' <i>United 93 </i>offers more...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Excuses for Bush's "war on terror"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 12, 2006 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>United 93</b></i>, written and directed by Paul Greengrass.</font><p>

<font face="Times New Roman, Times, serif" size="3">THERE'S NO question that <i>United 93 </i>is a well-made action film, with plenty of suspenseful if not terrifying scenes. And there's no question that Paul Greengrass is a talented filmmaker, whose documentary-style films make the viewer feel like they're actually witnessing real events.<p>
The question is remains, however, why make a film like <i>United 93?</i> <p>
Greengrass is best known for <i>Bloody Sunday, </i>the brilliant movie that chronicles the 1972 slaughter of unarmed Irish civil rights marchers by British troops in Derry, and <i>Bourne Supremacy,</i> the sequel to Hollywood spy thriller <i>Bourne Identity</i>.<p>
For his most recent film, he depicts events--some real and others made up--that took place on United Airlines Flight 93, one of four planes that were hijacked on September 11, 2001.<p>
The target of Flight 93 was the White House, but it instead crashed in a field outside Shanksville, Pa., after the hijackers lost control of the plane.<p>
The film begins with the four men as they prepare to hijack the plane. "I love you," says one man, as he boards the plane. This is where all sense of humanity ends when it comes to the Arab characters in the film, as they are transformed into murderers with knives, box cutters and a bomb, screaming in Arabic at weeping passengers.<p>
As the events on Flight 93 unfold in the air, Greengrass recreates the chaos on the ground as air traffic control officials come to realize that a plane, and later several planes, have been hijacked--and that they're on a suicide mission.<p>
The sense of chaos and disbelief is repeated in the military control room, where officers try in vain to contact the president, then the vice president, to get clearance to intercept Flight 93. They look on impotently as CNN broadcasts the first, then the second crash at the Twin Towers, and later at the Pentagon.<p>
In the end, Greengrass's overarching message is that the Bush administration failed the passengers of Flight 93. While the Bush administration stalled, the passengers were left to fight for their lives, which in the film is portrayed as a brave standoff--a fact unsubstantiated by actual evidence. (By the way, not all the passengers in the movie are depicted so bravely, especially a European passenger--maybe representing what Bush calls "old Europe"--who argues that they should do as the hijackers say.)<p>
True enough, as the 9/11 Truth Commission revealed, the Bush administration was utterly unprepared for September 11, even though they were well aware such an attack could likely take place. But it would be na&iuml;ve to think that this is the only message <i>United 93</i> will leave audiences with.<p>
Conservative pundit George Will suggests, "Going to see <i>United 93</i> is a civic duty...The movie may quicken our appreciation of the measures and successes--many of which must remain secret--that have kept would-be killers at bay...The message of the movie is: We are all potential soldiers. And we all may be, at any moment, at the war's front, because in this war the front can be anywhere.<p>
"The hinge on which the movie turns are 13 words that a passenger speaks, without histrionics, as he and others prepare to rush the cockpit, shortly before the plane plunges into a Pennsylvania field. The words are: 'No one is going to help us. We've got to do it ourselves.' Those words not only summarize this nation's situation in today's war but also express a citizen's general responsibilities in a free society."<p>
At its heart, <i>United 93</i> is a case for stepping up Bush's "war on terror"--ultimately for "vigilance" at the borders, for secret prisons where confessions are tortured out of detainees, and, yes, for racial profiling of Arabs and Muslims.<p>
Already, there have been reports of Muslims and Arabs being targeted since the film has come out. A week ago, three Muslim women wearing hijabs reported being confronted in a Scottsdale, Ariz., mall by a couple who said they'd just seen <i>United 93. </i>The two swore at the women and told them to get out of their country.<p>
Just few days ago, five men were pulled from an airplane in Newark and detained because they were "acting suspiciously"--they were reading flight manuals. They ended up being trainees--four in the Angolan military, and the fifth an Israeli--from a helicopter school in Dallas.<p>
Lewis Alsamari, an actor originally from Iraq but who now lives in Britain, played one of the hijackers in the film. He was himself refused entry into the U.S. for the film's premiere.<p>
In its first weekend out, more people went to see Robin Williams' road comedy <i>RV</i> than went to <i>United 93. </i>If paranoia is <i>United 93's </i>message, maybe this is as it should be.<p>

