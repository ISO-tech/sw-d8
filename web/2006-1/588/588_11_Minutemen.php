<font face="Times New Roman, Times, serif" size="4">Activists shout down "debate" at a church in Chicago</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Taking on the Minutemen</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Andrew Freund</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 12, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--More than 150 antiracist activists protested May 4 against the anti-immigrant vigilante group the Minuteman Project, shutting down their meeting. <p>
Originally, a member of the Minutemen was invited to debate an activist from the Illinois Coalition for Immigrant and Refugee Rights (ICIRR) at a church in Chicago's multiracial Logan Square neighborhood. The night before the event, however, the ICIRR backed out after it learned that a neo-Nazi group had put out a call to come support the Minutemen.<p>
But that didn't deter activists. While 60 demonstrated outside with chants like, "No Nazis, no Minuteman--we oppose your fascist plan," another 90 gathered inside the church.<p>
The church pastor didn't begin the meeting with a prayer, but with a singing of the national anthem. The pastor then announced that, since the ICIRR had withdrawn, a statement would be read on its behalf, and that Rosanna Pulido, the Latina spokesperson of the Illinois Minutemen, would then have the floor.<p>
But for protesters, this action was tantamount to giving racists a megaphone. "All the media is here. If no one came to oppose [the Minutemen], how would they do anything but grow?" said Saba Khan, one of the "Anti-Minutemen 5," a group of activists arrested in October 2005 for protesting against the Illinois Minutemen. <p>
Recently, Khan and her codefendants won a legal victory when, despite the prosecution's threats, they received comparatively light sentences of court supervision and community service in a plea deal.<p>
"We have to expose this illegitimate group and prevent their message of hatred from getting out. If they are here, we must be here," she said.<p>
Outrageously, church officials demanded there be no signs or disrespect in the "house of God." In response, a member of the audience called out: "How can you have Minutemen in here and say you're respecting God? Is your god a racist?"<p>
Protestors inside the church then began chanting loudly, and for 15 minutes the small sanctuary echoed with chants of "Hitler rose, Hitler fell, racist Minutemen, go to hell!" Police eventually declared the meeting cancelled, and the church was cleared. The only platform Pulido was allowed was a brief statement to the TV cameras, with demonstrators continuing to shout down the departing Minutemen as they skulked away. <p>
As this action showed, direct, confrontational protest is the most successful way to stop organizations that use "illegal immigration" as a cover for their ideology of racist hate.<p>

