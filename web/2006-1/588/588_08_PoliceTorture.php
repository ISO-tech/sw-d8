<font face="Times New Roman, Times, serif" size="5"><b>Justice for police torture victims!</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 12, 2006 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">ON APRIL 26, in its concern over the practice of torturing geese to make "foie gras," or goose liver pat&eacute;, the Chicago City Council voted to ban the sale of the delicacy in restaurants citywide. Mayor Richard M. Daley scoffed at the resolution. "We have real issues here in this city. And we're dealing with foie gras? Let's get some priorities," he said.<p>
I couldn't agree more. The prosecution of torturers on the Chicago police force under disgraced former Commander Jon Burge, and the redress of injustices for his victims, should be foremost among these priorities. Unfortunately, because Daley was Cook County State's Attorney throughout the 1980s, when over 100 African American men allege they were tortured, he has failed to address the "real issues here in this city."<p>
His office took statements from, and prosecuted, torture victims who had signed false confessions. Thirteen of these men were sentenced to death and countless others to lengthy prison terms. <p>
This widespread abuse and corruption led former Gov. George Ryan to pardon death row inmates and torture victims Madison Hobley, Leroy Orange, Aaron Patterson and Stanley Howard. As Howard has written, "[As] an ex-top prosecutor, Mayor Richard M. Daley gladly condoned and ignored the cries of torture because he was using the alleged confessions obtained by torture to build his political career." <p>
In 2002, special prosecutors Edward Egan and Robert Boyle finally were appointed to investigate the torture. After four long years, they have indicated that they will soon release their findings. <p>
If they do not bring indictments against torturers on the Chicago police force, I would hope that our public officials extend as much concern for human victims of torture as they do for geese. Justice demands it.<br>
<b>Julien Ball</b>, Campaign to End the Death Penalty, Chicago<p>

