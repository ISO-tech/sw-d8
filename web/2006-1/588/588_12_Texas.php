<font face="Times New Roman, Times, serif" size="4">Evidence of terrible injustice in Texas</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Executed and proven innocent?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Kathleen Feyh</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 12, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">"THE ONLY statement I want to make is that I am an innocent man, convicted of a crime I did not commit. I have been persecuted for 12 years for something I did not do." <p>
These are among the last words of Cameron Todd Willingham, executed by the state of Texas on February 17, 2004, for a crime that he always said--and that the New York City-based Innocence Project now says it can prove--he did not commit.<p>
Willingham was convicted of arson in a 1991 fire that killed his three children. At his trial, Willingham testified that he barely escaped the fire himself, and that he repeatedly tried and failed to rescue his children.<p>
His conviction rested on the testimony of deputy state fire marshal Manuel Vasquez, who concluded that the fire was set, and other testimony, later proven false, that pointed the blame at Willingham. However, a recent report by the Innocence Project, based on statements and analysis from leading arson experts, refutes the claim that arson caused the 1991 fire.<p>
And it shows that Willingham's conviction was based largely on junk science. Gerald Hurst, a private arson investigator, called the testimony that led to Willingham's conviction "a hodgepodge of old wives' tales," such as the idea that gasoline-fueled fires burn hotter than wood fires. This is wrong, say experts; gas blazes aren't necessarily hotter.<p>
Much of the information in the Innocence Project report was made available to the state of Texas before Willingham's execution. <p>
Around the time of the execution, another Texas death row prisoner accused of arson, Ernest Willis, was set free because of similar flaws in that case--fire officials claimed the fire was set, but the evidence didn't match up. Willis was exonerated and pardoned in October 2004, and collected almost $430,000 for 17 years of wrongful imprisonment.<p>
Despite Willis' exoneration and serious doubts raised about Willingham's guilt, the state of Texas carried out Willingham's execution anyway.<p>
Across the country, the death penalty is coming under increased scrutiny. Prosecutors are seeking--and juries are returning--fewer death sentences nationwide.<p>
Texas, however, continues to kill at a staggering rate--over a dozen executions are scheduled to occur between now and the end of August. Texas also leads the nation in inmates serving time for arson, according to the Innocence Project report--666 as of 2002<p>
Texas has a truly horrifying record of ignoring systemic inequalities of its justice system, leaving poorer defendants without the means of getting a fair trial, and railroading defendants of color to the death chamber.<p>
These new revelations of innocents on death row offer an opportunity to step up the fight to end the death penalty in Texas.<p>

