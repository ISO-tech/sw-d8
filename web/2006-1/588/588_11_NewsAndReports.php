<font face="Times New Roman, Times, serif" size="4"><b>We are millions strong</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Naveen Jaganathan</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BRIDGEPORT, Conn.--More than 400 immigrants and their supporters rallied May 6 outside the Federal Court House in Bridgeport, Conn., at a rally organized by the Catholic Church. Chanting "No somos diez, no somos cien. Somos millones. &iexcl;Cu&eacute;ntanos bien!" (We are not 10, we are not 100. We are millions, and don't forget it!) the protesters called for full rights and equality for immigrants.<p>
Despite being the biggest city in Connecticut and having a large immigrant population, Bridgeport had not seen any pro-immigrant rallies until the May 6 action. "This is only the beginning," said John Lugo of Unidad Latina en Acci&oacute;n, a Latino immigrant rights group based in New Haven, Conn. "There are lots of people in Bridgeport who want to organize around this and hopefully we can form a group after this demonstration."<p>
Supporters at the rally also included many local activists, and the newly formed Bridgeport Teachers for Peace and Justice.<p>
Activists also met on the same day for the monthly meeting of the Connecticut Regional Coalition for Immigrant Rights, an umbrella coalition of immigrant rights groups in the state. May 20 was chosen as the date of the next demonstration--a statewide rally for immigrant rights that will be held outside the Democratic Party's state convention. The rally will have two demands: "Equal rights for all immigrants" and "Unite and fight for jobs and justice." <p>
A resolution adopted at a meeting in April made it the official position of the coalition to not support guest-worker programs, instead pushing for full amnesty for all.<p>
The May 20 demonstration will be key to consolidating pro-immigrant forces in the state. We can send a strong message to Democratic Party politicians that we will settle for nothing less than full rights and equality for all immigrants.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Chicago Social Forum</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Susan Dwyer</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--The third annual Chicago Social Forum attracted more than 400 activists for a day of political discussion on May 5.<p>
This year's forum was cosponsored by 80 organizations as well as Roosevelt University's Mansfield Institute for Social Justice, which provided space and financial support. There were 57 workshops at the forum on topics ranging from building puppets to building antiwar coalitions.<p>
The two recent immigrant rights demonstrations in Chicago on April 10 and May 1, which drew out a combined total of nearly a million people, added urgency to the discussions. Activists involved with the coalition behind the demonstrations spoke on the panel "No Human Being Is Illegal: The New Movement for Immigrant Rights."<p>
The Southeast Chicago Coalition for Immigrant Rights, which brought 2,500 people to the April 10 demonstration, held a lively discussion on community organizing. One suggestion for the next step was a call for town hall meetings to encourage public debate on issues such as the guest-worker programs being called for in Congress.<p>
There were also community organizers involved in fighting the recent and planned school closings in Chicago as well as activists and coalitions fighting for prisoner rights and against the death penalty. Other panels focused on the environment, political change in Bolivian and Venezuela, and the Zapatista movement.<p>
In all, the Chicago Social Forum was a terrific success. It allowed activists and community members the time for political discussion and coalition building that will be invaluable in the march toward a better Chicago and a better world. <p>

