<font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Congress' two versions of immigration "reform" legislation:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Between bad and worse </b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 12, 2006 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">"NO TO HR 4437" has been the rallying cry for millions of people who marched for immigrant rights these past weeks.<p>
HR 4437 is the official designation of anti-immigrant legislation, sponsored by Rep. James Sensenbrenner, that was passed by the U.S. House last December. It brands undocumented immigrants, along with anyone who aids them, as felons. But there is another danger--a "compromise" shaping up in the Senate that would also make immigrants' lives worse.<p>
The Senate deal is being dressed up with rhetoric about tolerance and realism, and it has the support of many Democrats and some mainstream immigrant rights organizations. But undocumented immigrants would be better off if none of the proposals under consideration in Washington become law.<p>
"There's a big gap between what advocates in D.C. are negotiating and what [immigrant] communities are really demanding," Arnoldo Garcia, of the National Network for Immigrant and Refugee Rights, told the <i>Wall Street Journal</i>.<p>
The Senate "compromise" bill, negotiated by Republican Sens. Chuck Hagel and Mel Martinez, broke down before Congress' spring recess last month, but leaders of both parties say they want it reintroduced, as early as this week.<p>
The Hagel-Martinez proposal divides the undocumented into three groups, based on how long they've been in the U.S.--something that organizers of the demonstrations reject. Only those who can prove they've been in the U.S. more than five years would be allowed to remain uninterrupted, and begin a twisted "path to citizenship" that would take at least five more years.<p>
Now, other landmines buried in the Hagel-Martinez "compromise" are coming to light as activists get a look at the fine print.<p>
For example, the Sensenbrenner bill would make it a deportable offense if undocumented workers gave false information (such as a fake Social Security number) to get a job at any point, while Hagel-Martinez would penalize this only if it came to light after the bill becomes law--but, say legal experts, immigrants who keep working for the same employer could face deportation if they had earlier used false papers to get the job.<p>
There are other cruelties hidden in the legislation, too--such as a plan to add the names of anyone who violates immigration rules to the national crime database.<p>
This "compromise" started out as a proposal by Sens. John McCain and Ted Kennedy. In addition to many border enforcement provisions in common with the Sensenbrenner bill, McCain-Kennedy also included a restricted "path to citizenship" for some of the 12 million undocumented workers in the U.S.<p>
Immigrants following the "path" would have years of legal limbo ahead of them, plus payment of a penalty and back taxes--not to mention fees that, together with the fines, would put legalization out of reach for the poor. And they would have to stay employed--giving management something to hold over the heads of any who speak up for their rights on the job.<p>
The McCain-Kennedy proposal also included a business-backed guest worker program to bring temporary workers to the U.S., but relegated to second-class status--legally tied to the employers that recruited them, but without the rights afforded other workers to challenge unfair conditions, and subject to deportation if they leave their job.<p>
So even in its original form, McCain-Kennedy fell short of equality and fair treatment for undocumented workers.<p>
But since it was introduced in the Senate, even worse provisions--some of them taken straight out of Sensenbrenner--have been loaded on to make the legislation more appealing to right-wingers.<p>
The Senate "compromise" doesn't deserve the support of the millions of people who marched for immigrant rights. "We can't be fooled into believing there's a breakthrough when proposed measures actually make undocumented immigrants ineligible," Eun Sook Lee, director of the Los Angeles-based National Korean American Service and Education Consortium, told the <i>Wall Street Journal</i>.<p>
Unfortunately, some mainstream immigrant rights organizations--speaking in the name of the millions-strong demonstrations--support the Senate "compromise." They say that the "realistic" course is to support the less repressive Senate deal against HR 4437.<p>
But it isn't "realism" to support this compromise. It is advocating something less than justice and equality, and that is not acceptable.<p>

