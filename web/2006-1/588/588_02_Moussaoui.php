<font face="Times New Roman, Times, serif" size="4">Jury rejects death sentence for Moussaoui</font> <br>
<font face="Times New Roman, Times, serif" size="5"><b>Feds' show trial fails</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 12, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">DESPITE THE federal government's best efforts to send him to the execution chamber, a jury last week sentenced Zacarias Moussaoui to life in prison.<p>
Moussaoui, an admitted al-Qaeda member, is the only person to have been charged in the U.S. in connection with the September 11 attacks. <p>
Moussaoui was locked up on an immigration violation when the September 11 attacks occurred. Then-Attorney General John Ashcroft--under pressure to charge someone in connection to September 11 attacks--initially alleged that Moussaoui was supposed to be the 20th hijacker. <p>
Later, the government dropped that claim and instead argued that Moussaoui should be executed for what he <i>didn't</i> do--for failing to tell authorities while he was in jail that al-Qaeda was planning to use planes to attack U.S. targets.<p>
From the beginning, Moussaoui's trial was a circus. The media focused on Moussaoui's repeated courtroom insults and outbursts--including his bombshell testimony that he had plotted with so-called "shoe-bomber" Richard Reid to hijack a plane and fly it into the White House on September 11. But even prosecutors later admitted that this story wasn't true--the FBI investigated and rejected the claim.<p>
Throughout the trial, Moussaoui showed signs of mental illness. One psychologist testified that Moussaoui was suffering from paranoid schizophrenia, and believed that President Bush would set him free and that his court-appointed lawyers were plotting to kill him. In spite of questions about his competency, Moussaoui was allowed to testify, against his lawyers' advice. <p>
Federal judge Leonie Brinkema allowed the case to proceed even after it was discovered in March that Transportation Security Administration lawyer Carla Martin had coached prosecution witnesses in the case.<p>
During the penalty phase of the trial, Brinkema allowed a cockpit recording of United Flight 93, which crashed into a Pennsylvania field on September 11, to be played for the first time publicly. Videotape of victims jumping to their deaths from the World Trade Center was also played in court. And the prosecution was allowed to call family members of September 11 victims, as well as former New York City Mayor Rudolph Giuliani, as witnesses to press for the death penalty.<p>
Even as they sentenced him to life in prison, a note from three of the jurors said they believed Moussaoui had only limited knowledge of the September 11 attacks--a contradiction of their earlier ruling.<p>
But the fact that the jury decided to spare Moussaoui's life is a setback for the Bush administration--which has been pressing for convictions in its "war on terror."<p>
As David Bruck, a federal capital defense lawyer, told the <i>New York Times</i>, "There has never been a case where the connection between the defendant and the killing was anywhere near as weak as here. If this hadn't been 9/11, the government's theory of liability would not have passed the laugh test."<p>

