<font face="Times New Roman, Times, serif" size="5"><b>Puerto Rico's crisis sets stage for confrontation</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Roberto Barreto</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 26, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE GOVERNMENT of Puerto Rico declared itself bankrupt and laid off 95,000 public employees for two weeks beginning May 1, sparking a series of marches and protests by organized labor, students and others.<p>
Gov. Anibal Acevedo Vil&aacute; and the legislature couldn't agree on fiscal and tax reform proposals. Acevedo's Popular Democratic Party (PPD, according to its initials in Spanish) wanted to impose a 7 percent sales tax.<p>
The legislature, controlled by the pro-statehood New Progressive Party (NPP), originally proposed the measure. But the NPP insisted that the tax be limited to 4 percent, seeking to prevent Acevedo from developing public works that could help him win the 2008 elections.<p>
Both parties are pro-business. In the last year, the government has raised tuition at public universities, doubled the price of water and electricity, raised toll fees, laid off hundreds of workers and allowed the unregulated free market to determine the price of gas.<p>
Now, the government has declared a fiscal crisis--after years of allowing tax evasion by corporations and rich individuals. In fact, investigations by the newspaper <i>El Nuevo Dia </i>demonstrated that the amounts owed by tax evaders are actually greater than the deficit.<p>
Yet instead of cracking down on tax evaders, the government increased the public debt by cutting capital gains taxes from 20 to 12.5 percent.<p>
Last year, Acevedo proposed laying off thousands of government workers--or cutting their work hours by 20 percent--as a "solution" to the crisis. The labor movement mobilized then, but failed to act with strength.<p>
Then on May 1, a partial government shutdown locked out 95,000 public-sector workers. The reaction of unions was so slow that the call for action came from two radio disc jockeys, who organized a 45,000-strong march against the closure.<p>
Eventually, the unions mobilized, but failed to present an independent opposition to the bosses' offensive, instead taking the side of Acevedo against the NPP-dominated legislature.<p>
The exception was the Coordinadora Sindical--which includes the teachers unions, the FMPR, and the electrical workers union, the UTIER. On May Day, the Coordinadora rallied against the big banks and corporations, and on May 5, it organized a march on the governor's mansion and called for a general strike.<p>
Yet the unions involved hadn't consulted their memberships about the action, and the majority of teachers and electrical workers didn't answer the strike call. The teachers' union president, Rafael Feliciano, followed up with a picket at the Port of San Juan, demanding that cruise ships pay taxes, but only 200 people participated.<p>
Other union leaders, such as Jos&eacute; Rodriguez from the Federaci&oacute;n de Trabajadores, maintained that they were opposed to a sales tax, but said they would accept the tax as a means to return to work.<p>
A deal between the governor and the legislature ended the crisis with an emergency loan to finance state operations while a commission finds a permanent "solution." But a big confrontation between the government and workers is still likely, as the government has used the crisis to refuse to negotiate 40 pending collective bargaining agreements with unions.<p>
Although the call for a general strike was a fiasco, some good came out of it. The workers' councils active during the Peoples' Strike of 1998 against privatization have been reactivated.<p>
It is urgent to rebuild the labor movement in Puerto Rico--and critical and democratic debate is a necessary condition for its reconstruction.<p>

