<font face="Times New Roman, Times, serif" size="4"><b>Defend Harlem from gentrification</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Hannah Wolfe</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--"We damn sure better be together on this," said Florence, an 87-year-old resident of Harlem evicted from her apartment more than a year ago and still fighting for her rights in court.<p>
More than 100 Harlem residents, many elderly, all angry and vocal, packed a meeting hall at the Adam Clayton Powell State Office Building May 17 to fight back against the recent onslaught of rent hikes, illegal evictions, and predatory real estate practices in the "hot" new real estate market of Harlem. <p>
"Hurricane Gentrification" has hit Harlem hard, leaving in its wake thousands of tenants evicted from publicly and privately owned affordable housing. "Housing court offers little if any relief since it only serves as a collection agency for landlords," noted meeting organizer Nellie Bailey.<p>
Since gentrification began 20 years ago, Harlem housing costs have increased by an astonishing 500 percent. At the same time, unemployment rates continue to rise, with more than 50 percent of Black men in Harlem are unemployed.<p>
Bailey, activist director of the Harlem Tenants Council; Kim Powell of Buyers and Renters United to Save Harlem (BRUSH); and the East Harlem immigrant housing rights group Mirabal Sisters organized the meeting in response to the recent public housing rent increases, exorbitant "maintenance" surcharges, aggressive real estate corporations buying up Harlem housing stock, and citywide relaxation of rent-control measures.<p>
The toll this is taking was evident in the testimony of Harlem tenants at the meeting. One after another, they stood and shared stories of landlord harassment, illegal evictions and housing courts packed with frail and elderly people afraid of losing their homes.<p>
They urged one another not to be afraid to fight back, and they cautioned against falling into the trap of landlords' "divide-and-conquer" tactics. "Immigrants are not our enemies, but our fellow victims," said Bailey. <p>
The meeting ended with a spontaneous call for a Harlem rent strike, which met with enthusiastic support, and a committee was formed to start organizing. "If we don't fight back, we will no longer have a community," said Bailey. "Lord knows the Democratic Party is not the answer to our woes. We have to organize ourselves."<p>
Meeting participants also are planning an anti-gentrification "feeder" rally and march to join up with the June 10 Affordable Housing March at 135th and Broadway. <p>

<font face="Times New Roman, Times, serif" size="4"><b>Solidarity with Egyptian activists</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Mostafa Omar</b></font><p>
<font face="Times New Roman, Times, serif" size="3">ON MAY 25, demonstrations will take place outside Egyptian consulates in London, Paris, New York City, San Francisco, Chicago and Washington, D.C., to mark an international day of solidarity with Egypt's growing movement for democracy. <p>
The government of Egyptian President Hosni Mubarak has escalated its campaign of repression against pro-democracy protesters in recent days. On May 19, police forces violently dispersed a peaceful demonstration of 1,500 that had gathered to show solidarity with two pro-reform judges on trial for challenging the legitimacy of last November's parliamentary elections among allegations of vote fraud. Police arrested more than 300 protesters, increasing the number of recent political prisoners to more than 600. <p>
The U.S. State Department issued a statement rebuking the Egyptian government's repression, however, in a hypocritical move, Congress will not suspend its $2 billion in aid to the Mubarak regime--a key U.S. ally. All those who would like to see a Middle East free from dictatorships as well as U.S. domination are encouraged to attend the upcoming demonstrations. <p>
