<font face="Times New Roman, Times, serif" size="5"><b>Will the Enron crooks go to jail?</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 26, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">WILL ENRON'S top executives go to prison for presiding over massive corporate fraud? After four months of testimony, a jury began deliberating May 18 on federal fraud and conspiracy charges against Enron founder and former chair Ken Lay and ex-CEO Jeffrey Skilling.<p>
The case finally went to the jury more than four years after the Houston-based energy company's spectacular collapse in bankruptcy.<p>
Skilling faces 28 counts of fraud, conspiracy, insider trading and lying to auditors--which would land him a maximum sentence of 275 years in prison if convicted on all counts. Lay is up for six counts of fraud and conspiracy, with a possible combined maximum punishment of 45 years behind bars.<p>
Meanwhile, Lay was facing a separate trial on charges that he obtained $75 million in loans from three banks and reneged on an agreement not to use the money to buy stock.<p>
Lay's signature appeared on forms promising that he wouldn't use credit on a loan to purchase stocks. In his defense, Lay's attorneys say that he wasn't responsible--because his assistants used an "auto-pen" to sign his name.<p>
The Enron bosses' corporate crimes took place in a system that is already rigged in their favor. As Lay's own lawyer pointed out, "Why are they trying to criminalize this? We might as well put every CEO in jail."<p>
Enron employees have already paid the price for Enron's fraud. "I was the one left holding the bag, but Lay and Skilling are able to get their day in court," said Debra Johnson, who worked at Enron from 1994 until it went bankrupt in 2001.<p>
Johnson told CNN, "I'm the one left with no insurance. I don't have anything. Nothing. Skilling and Lay don't know what it's like to go get food stamps and welfare while looking for employment full time. It's awful. Can I send my rent bills to Mr. Lay and Mr. Skilling?"<p>
Several state workers' pension funds, including New York's, were tied up in Enron stocks--and when the company went down the tubes, so did state workers' retirement money.<p>
In their defense, Skilling's and Lay's lawyers claimed that their clients had been traumatized. Skilling is, in the words of his lawyer Daniel Petrocelli, a "tortured soul" because of "what happened to the business he built and what it will be known as."<p>
"Look into his soul," Petrocelli added in his closing arguments. "See if you see a criminal." Debra Johnson has done just that--and concluded, "I'd like to see them do hard time."<p>

