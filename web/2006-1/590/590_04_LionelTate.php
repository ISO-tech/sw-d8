<font face="Times New Roman, Times, serif" size="5"><b>A young man's life is being destroyed</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 26, 2006 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">IN 2001, 14-year-old Lionel Tate was sentenced to life in prison for the death of a playmate--6-year-old Tiffany Eunick, who was killed accidentally in 1999 when Lionel, then 12, acted out a wrestling move. <p>
Originally sentenced to life in prison without parole, Lionel's conviction was eventually reversed after a public outcry against such harsh sentence for a juvenile. Lionel eventually pled guilty to second-degree murder in exchange for a sentence of one year's house arrest and 10 years' probation. <p>
Last week, Lionel, now 19, was sentenced to 30 years in prison for violating his probation in relation to a robbery of a pizza delivery driver. Lionel's godmother THERESA JOHNSON sent this message to <i>SW</i>, explaining the hurdles that Lionel has faced from the police and courts.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">I AM writing to you in hopes that someone will hear the truth about what transpired in a south Florida courtroom. Judge Joel T. Lazarus sentenced Lionel Tate to 30 years in prison--for allegedly possessing a gun in February 2005, almost three months prior to the robbery of a pizza deliveryman. <p>
This did not come to the attention of authorities until five months later, after he was in custody for the robbery. In a crowded neighborhood, no one heard the shots, called police or anything, which is curious. The "evidence" the Broward County Sheriff's Office has consists of coerced statements given under duress, by "witnesses" who are suspects themselves in the robbery case.<p>
It is no secret some in the sheriff's office believe that Lionel Tate should never have been granted a second chance and want him returned to prison. That fact has a great deal to do with what transpired. Since his release from prison, Lionel has been repeatedly harassed and watched by the agency--they have been waiting for an opportunity like this.<p>
There is a great deal of evidence to support my claims. The most difficult part is trying to obtain legal counsel that isn't beholden to the sheriff's office, and that is impossible at a local level. Please help if you can. A young man's life is being systematically destroyed in front of the world, and no one has come forward to help stop it.<p>

