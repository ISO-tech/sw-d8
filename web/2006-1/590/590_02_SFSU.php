<font face="Times New Roman, Times, serif" size="4">SFSU students fight campus administrators' crackdown</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>An attack on the right to protest</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 26, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>KAREN KNOLLER reports on attempts by campus administrators to silence antiwar activists in San Francisco.</b><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO State University (SFSU) administrators are stepping up their attack on student antiwar activists, but the students are fighting back. <p>
The university's office of judicial affairs has targeted 10 individuals in an attempt to intimidate, divide and stifle protest on campus. The students each received letters requesting confidential meetings to "investigate" a complaint filed by Chief of Public Safety Kim Wible regarding an April 14 counter-recruitment protest.<p>
That day, students gathered at the school's career fair to protest the presence of military recruiters and the war in Iraq. Their activities included questioning recruiters and talking to potential recruits, distributing antiwar literature, and chanting while holding up signs. The students were loud but peaceful. <p>
Soon after the chanting began, however, a wall of police suddenly confronted the 10 protesters, forcibly removing them from the fair. The students were issued no warning--simply cited for "disrupting university activity." <p>
The citation, sanctioned by university President Robert Corrigan, barred them from campus for two weeks and threatened the activists with immediate arrest and a fine if they returned within that period. Three of the students live on campus and were in effect made homeless as a result of the citation.<p>
In response, students held an April 17 press conference to defend their actions and protest the supposedly liberal SFSU administration for infringing on students' basic rights. The flood of phone calls and e-mails to Corrigan in support of the activists forced the administration to back down somewhat. Later that day, all 10 students received word that they were allowed back on campus. <p>
More than 1,000 people, including many prominent antiwar activists such as Cindy Sheehan and Dahr Jamail, have signed an open letter of support and solidarity. Denis Halliday, the former United Nations (UN) humanitarian coordinator in Iraq, who resigned this position in protest because of the humanitarian crisis caused by U.S.-UN sanctions, also signed on to the letter.<p>
Despite the support, future disciplinary action still looms. Wible wrote a response to the letter of solidarity that includes a litany of lies aimed at smearing the students. <p>
She claims, for example, that the director of the career fair and a police officer asked students to leave before the police cited them, and she denies there was any police misconduct. This simply isn't true--and there are photographs of the event that show police aggression. <p>
Wible even has the audacity to claim that "the university remains committed to the ideals of free speech." Wible must be referring to the "free speech" that students are allowed to exercise only in designated zones, from noon to 2 p.m. The hypocrisy of this university, which commemorates and celebrates people like Malcolm X, Rosa Parks and Cesar Chavez, is astounding.<p>
With the war in Iraq plunging in popularity and taking ever more lives of both Iraqis and U.S. soldiers, the military faces an uphill battle to fill its ranks. It is imperative that students be allowed to protest without fear of reprisal from police or school officials. Student activists at SFSU will not be intimidated.<p>
<i>Send letters in defense of students' rights to SFSU President Robert Corrigan at <a href="mailto:corrigan@sfsu.edu" target="_blank">corrigan@sfsu.edu</a>, or call him at 415-338-1381</i>.<p>

