<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Isn't socialism about conformity?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 26, 2006 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">SOCIALISM WILL make us all like the same. This claim is, to quote the British journalist Paul Foot, "delicately stoked by the press and television of a capitalist society which is increasingly stamping sameness and conformity on its working people."<p>
The claim was buttressed by images and stories of people in the so-called communist regimes wearing identical clothing; schoolchildren mouthing slogans by rote to the great leader' highly regimented work and leisure time; row upon row of drearily identical buildings; and the absence of any public debate. <p>
But the system of totalitarian conformity that Mao and Stalin imposed in the Soviet Union and China respectively had nothing to do with socialism. Rather, it was part of a policy designed to regiment and dragoon the population, as the military does its soldiers, into making the maximum exertion for the purposes of building up these countries' industrial and military strength. <p>
This swift, blitzkrieg-style industrialization required strict labor discipline. Along with that went an ideology emphasizing national unity and strict conformity to the dictates of the party bureaucracy, centered around a deified leader--the infamous cult of personality.<p>
"Thank you, Comrade Stalin, for our happy childhood," Russian students were required to say in class. One Chinese official sang this "hymn of praise" to Chairman Mao at the 1955 National People's Congress: "The sun shines only in the day, the moon shines only at night. Only Chairman Mao is the sun that never sets."<p>
The West had its own brand of stifling conformity during the Cold War--the climate of fear surrounding the McCarthyite anticommunist witch-hunts and the sterile clich&eacute;s offered up by film, television and advertising of prosperous, happy (and white) suburban family life that never conformed to reality, for instance.<p>
A 1957 novel expresses some of the frustration felt at the sameness of mass produced suburban tract housing: "In any one of these new neighborhoods, be it in Hartford or Philadelphia, you can be certain all other houses will be precisely like yours, inhabited by people whose age, income, number of children, problems, habits, conversation, dress, possessions and perhaps even blood type are also precisely like yours."<p>
Mass-market capitalism imposes conformity on the majority. This is true at work and outside work.<p>
At work, we are expected to follow strict rules about our time and behavior; we are expected to follow a dress code. We churn out the same product, day in and day out, with no let up, and we have very little control over the process.<p>
Outside of work, we are treated as "mass consumers"--coaxed, exhorted and seduced into showing up in our millions to buy exactly the same product.<p>
Only the wealthy have the money and leisure to have palaces built by craftsmen, send their kids to the best schools, and take exotic vacations in private jets. Socialization is not about conformity or sameness, but about creating the basis for the flowering of creative expression and individual achievement.<p>
In a socialist society, the productive power of society is harnessed to serve everyone's needs. We have the technology today, if we replaced the market with democratic planning, to end unemployment and shorten the workday to six or even fewer hours--creating the leisure time that would allow the majority to pursue all sorts of dreams that today are out of reach to all but a minority.<p>
Trotsky wrote, "Spiritual creativeness demands freedom. The very purpose of communism is to subject nature to technique and technique to plan, and compel the raw material to give unstintingly everything to man that he needs. Far more than that, its highest goal is to free finally and once for all the creative forces of mankind from all pressure, limitation and humiliating dependence.<p>
"Personal relations, science and art will not know any externally imposed 'plan,' nor even any shadow of compulsion. To what degree spiritual creativeness shall be individual or collective will depend entirely upon its creators."<p>

