<font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Deadly toll of anti-immigrant racism</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 26, 2006 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">ON MAY 18, U.S. Border Patrol and Customs agents killed 22 year-old Oscar Abraham Garcia-Barrios--just 50 feet north of the border crossing from San Diego to Tijuana. He was transporting four undocumented immigrants--back to Mexico.<p>
Border Patrol agents said they followed Garcia-Barrios' Dodge Durango after receiving a "tip" from a U.S. "citizen" that he had picked up undocumented Mexicans. <p>
Armed agents surrounded the vehicle as it stopped in the far right lane, 100 feet north of the Mexican border. Agents smashed the driver's side window when Garcia-Barrios refused orders to get out of the car. They opened fire when he then accelerated in the direction of the border. <p>
Garcia-Barrios died at the scene of multiple gunshot wounds. The agents refuse to say how many rounds they fired. They had made no attempt to shoot at the vehicle's tires. No weapons were found in the Dodge Durango.<p>
Expect to hear more stories like this one in the future. Both sides involved in the congressional furor over immigration reform are jostling for red-state votes--mirroring the level of xenophobia and racism long associated with organizations of the far right.<p>
It is therefore not surprising that far-right organizations are beginning to grow significantly. A report by the Southern Poverty Law Center documented 803 hate groups operating in the U.S. last year--33 percent more than in 2000. The Border Guardians, an organization based in Arizona, is accused by the center of working with neo-Nazis to harass and steal money from undocumented immigrants.<p>
Border Guardians' director Laine Lawless vehemently denied those charges. But she also called immigrant rights activists "brown Nazis" who are threatening to ignite a "civil war" in America, leaving considerable doubt as to whether she is capable of making such a political distinction.<p>
Speaking of illegality, it might surprise Lawless to learn that the entire Southwestern U.S., including her home state of Arizona, was part of Mexico until the U.S. military invaded that sovereign nation in 1846 to force the Mexican government to "sell" one-third of its territory for a paltry sum. <p>
By 1847, many U.S. politicians were clamoring to annex all of Mexico. But Southern slaveholders won out. Sen. John Calhoun of South Carolina expressed the majority's objections in an 1848 congressional debate: "To incorporate Mexico, would be...incorporating an Indian race; for more than half of the Mexicans are Indians, and the other is composed chiefly of mixed tribes...Ours, sir, is the Government of a white race."<p>
When Congress created its first broad immigration controls in the 1920s, Mexicans were singled out for the first "guest-worker" programs, admitted and then forcibly returned when their labor was no longer needed. <p>
Immigration laws have undergone many changes since, but this employment pattern for Mexican workers has remained. Even when federal law bans Mexican migration, immigration officials reserve punishment for undocumented workers, deported in showcase immigration raids--to reinforce an atmosphere of fear in Mexican communities.<p>
This was the case even during the notorious bracero program, which imported more than 4 million Mexican farm laborers between 1942 and 1964--all deported by their employers when their contracts expired.<p>
But while the bracero program was in full swing, the U.S. Immigration and Naturalization Service also instituted a mass deportation program in 1954, known as "Operation Wetback." The INS deported 1 million Mexicans in the name of "protecting national security and American jobs."<p>
U.S. employers had found an ideal solution--two sides of the same coin: rigorous border enforcement and guest-worker status for Mexican migrants. Both these elements--and only these elements--are present in the congressional debate over immigration now being waged in Washington. <p>
Democratic Sen. Dianne Feinstein has suggested that upcoming negotiations with the House will "not necessarily" be finished before the November elections. Let's hope so. No legislation is far preferable to what is on offer. <p>
The future for immigrant rights will be determined not by election-year congressional wrangling but by more bodies in the streets.<p>

