<font face="Times New Roman, Times, serif" size="5"><b>Driven to suicide by U.S. torturers</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 26, 2006 | Pages 1 and 2</font><p>

<font face="Times New Roman, Times, serif" size="3">AS THE United Nations (UN) called for the U.S. prison camp in Guant&aacute;namo Bay, Cuba, to be closed, the horrific toll that indefinite detention is exacting on prisoners became even clearer.<p>
On May 18, up to four desperate inmates were driven to try to commit suicide, overdosing on hoarded anti-depressants.<p>
The same day, inmates in another section of the prison camp attempted to prevent guards from interfering with a detainee who was attempting to hang himself. A group of about 10 detainees reportedly confronted guards from the so-called "Extreme Reaction Force" who were trying to prevent the suicide attempt. <p>
The detainees reportedly used improvised weapons made from fan blades, metal sheeting and closed-circuit television cameras that had been ripped from the walls--.<p>
The guards retaliated with batons and shields, doused inmates with pepper spray, fired rubber bullets from shotguns, and used at least one "sponge grenade"--a piece of metal or plastic covered in a spongy material that is designed to incapacitate crowds by inflicting "blunt trauma."<p>
It was "probably the most violent outbreak" in the camp's four-year history, claimed Rear Admiral Harry Harris, the U.S. commander of the detention center. <p>
According to Harris, the hanging attempt was no more than a ruse to lure guards into an attack. "These are dangerous men and determined jihadists," he said.<p>
But a new UN report makes it clear that indefinite detention at Guant&aacute;namo, along with the brutal conditions prisoners are subjected to, has led many of the prisoners to take increasingly desperate action to end their lives.<p>
The report--authored by the UN Committee on Torture, which is made up of 10 independent human rights experts--criticized interrogation techniques employed by the U.S. at Guant&aacute;namo, as well as the Bush administration's practice of using secret prisons around the globe to house an undisclosed number of "terrorist" suspects and shipping prisoners to countries that allow torture for interrogation.<p>
According to the report, the U.S. use of interrogation techniques like "water boarding" (a form of mock drowning in which a detainee is strapped to a board and held under water), as well as the use of sexual humiliation and dogs to frighten prisoners, constitutes inhumane treatment and possibly torture, and is a clear violation of international law. <p>
The U.S. "should cease to detain any person at Guant&aacute;namo Bay and close this detention facility, permit access by the detainees to judicial process or release them as soon as possible," reads the UN report.<p>
The Bush administration claims that it wants to shut down Guant&aacute;namo--but can't, in part because it fears detainees might be "mistreated" by other countries. "We work almost daily with governments to try to get people returned to their native lands if their governments will take them and give assurances that they are both not going to be mistreated and that they're going to be watched and monitored so that they can't commit crimes again," claimed Secretary of State Condoleezza Rice on <i>Fox News Sunday</i>.<p>
But the findings of the UN Committee on Torture show that it is the U.S. that subjects prisoners to routine mistreatment. As Joshua Colangelo-Bryan, an attorney for a detainee from Bahrain who has repeatedly tried to kill himself, told the Associated Press, "Under these circumstances, it's hardly surprising that people become desperate and hopeless enough to attempt suicide."<p>

