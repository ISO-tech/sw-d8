<font face="Times New Roman, Times, serif" size="4">Crackdown sparks international protests</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Egypt's drive to crush dissent</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Mostafa Omar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 2, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">HUNDREDS OF people protested outside Egyptian embassies in six different countries May 24 and 25 to demand that the Egyptian government cease its campaign of violent repression against a growing pro-democracy movement. <p>
Demonstrations took place in Egypt as well on May 25, marking the one-year anniversary of what pro-democracy activists call Black Wednesday--the day police thugs beat and sexually assaulted dozens of women protesting President Hosni Mubarak's sham referendum to keep himself in power.<p>
Once again, police attacked the protest--the latest assault on the series of demonstrations sparked by the regime's attempts to discipline judges who have challenged election fraud.<p>
According to a statement by the pro-democracy Kefaya (Enough) movement, two activists--Mohammed al-Sharqawy and Karim al-Shaer--were kidnapped off a street near the rally site. The two are Youth for Change activists, and had just been released a few days before following nearly a month in detention.<p>
An Associated Press reporter saw al-Sharqawy grabbed by more than a dozen men, and beaten and kicked. The two were taken to a police station, where security officers sodomized al-Sharqawy, using a roll of paper, according to the Kefaya statement. "According to lawyers," says the statement, "there is nearly no area on al-Sharqawy's body that is void of bruises, swelling or injury." <p>
Al-Sharqawy and al-Shaer were ordered locked up again for another 15 days for violating a ban on participating in demonstrations.<p>
Shortly after the two activists were abducted, police smashed in the windows of a car carrying three women--two of them journalists and the other an activist, according to press reports. Despite the fact that one of the women is six months' pregnant, the police dragged the three women out of the car, before later releasing them.<p>
According to Agence France-Presse, several of the hundreds of detainees rounded up in the Egyptian regime's latest crackdown have begun a hunger strike, demanding an investigation into the allegations of torture and the release of political detainees. "Our continued detention proves the regime's fear and terror of people's opposition to its corruption, dictatorship and its systematic destruction of the country," read a statement from the detainees posted on the Internet.<p>
The pro-democracy activists are gaining increasing international support. Last week's actions in the U.S. saw pickets in New York, Washington, D.C., Chicago and San Francisco, bringing out as many as 100 people in total. Activists in Washington and New York are planning meetings to coordinate further actions.<p>
