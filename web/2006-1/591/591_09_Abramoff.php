<font face="Times New Roman, Times, serif" size="5"><b>The Republican patronage machine</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 2, 2006 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">WITH EACH new day, more bad political news seems to befall the Bush administration and Republicans in Congress.<p>
Major national opinion polls estimate that only about one-third of Americans support President Bush and the Republicans in Congress. It's gotten so bad that even Ken Mehlman, chair of the Republican National Committee, was said to have told congressional leaders that the GOP could lose 25 seats in the U.S. House, giving the Democrats a majority.<p>
Topping the list of voter concerns driving this discontent with Bush and the GOP is, of course, the war in Iraq. Since early 2005, growing majorities of Americans have told pollsters that they believe the war "wasn't worth it" and that they want to see U.S. troops withdrawn soon.<p>
Added to this are economic issues, from rising gas prices to affordability of health care. A May <i>Washington Post </i>poll showed that 69 percent surveyed believe the nation is "off track.<p>
For these reasons, the same <i>Washington Post </i>poll showed 56 percent of the public preferring a Democratic Congress in November, with three times as many people saying they would use their vote to express opposition to Bush rather than support for him.<p>
Since taking Congress in 1994 and the presidency in 2001, the GOP has managed to exert a pull on politics to the right that has been largely successful, despite governing with fairly slim majorities in Congress. Bush himself lost the popular vote in 2000 and won only 51.1 percent of the vote in 2004, the narrowest re-election victory of a president since 1916.<p>
These are hardly ringing endorsements of Republican tax cuts for the rich, eternal war and corporate welfare from the majority of the American public. Yet the GOP machine has been extremely effective in pushing through its right-wing agenda--despite popular opposition or indifference to the issues that excite the GOP's conservative "base."<p>
One key reason why the GOP has been so effective is the back story to the scandal involving GOP lobbyist Jack Abramoff, who pleaded guilty to a number of federal charges early this year.<p>
Abramoff wouldn't have been in the position to exploit his connections if he wasn't part of the architecture of Republican rule in Washington. Abramoff was a cog in Tom DeLay's (R-Texas) "K-Street Project," a plan to enforce Republican hegemony on the lobbying industry that had a "bipartisan" approach to Congress and the executive branch before the GOP won the Congress in 1994.<p>
As a cost of doing business with the new GOP majority, DeLay and his henchmen demanded that corporations and lobbying firms hire Republican loyalists. Or, in the case of Abramoff's firm, that they funnel their business through the firm of a Republican loyalist.<p>
Abramoff had been a long-time GOP activist from the days when he knew Karl Rove and Grover Norquist, a consigliore of the GOP-linked conservative activist groups as president of Americans for Tax Reform, from their days in the leadership of the College Republicans.<p>
Abramoff's shakedowns of Indian tribes weren't really for services rendered so much as they were used to create a slush fund that made available millions of dollars for the congressional leadership, namely DeLay and Sen. Rick Santorum (R-Pa.), to distribute to Republican politicians in both Houses. In this way, Abramoff worked with DeLay, even sharing staffers, to build the patronage machine that assured that DeLay would almost always have the votes he and his errand boy, House Speaker Dennis Hastert (R-Ill.), needed to pass through the GOP-corporate agenda.<p>
The Center for Responsive Politics estimates that Abramoff-connected money amounts to $55 million spread among 300 members of Congress.<p>
If the Abramoff scandal succeeds in bringing down enough members of the Republican leadership to seriously threaten this patronage machine, then Republican control of the congressional agenda really will be under threat. And, given the near-complete lack of a Democratic opposition, this would be a blow to the GOP that would be entirely self-inflicted.<p>

