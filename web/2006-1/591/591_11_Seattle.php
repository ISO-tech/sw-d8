<font face="Times New Roman, Times, serif" size="5"><b>Protesting Seattle's school closings</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Steve Leigh and Vicky Jambor</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 2, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--Some 2,500 parents have rallied to oppose school closures here throughout the week ending May 28. In a half-dozen public meetings, parents, teachers and community members denounced the proposal of the Citizen Advisory Committee to close 12 schools.<p>
The parents were articulate and organized. Each school made its own T-shirts in a different color, turning the meetings into a beautiful sea of orange, green, yellow and purple shirts. Parents rose to explain why their school should be saved from the ax.<p>
At one meeting, 50 parents from Thurgood Marshall Elementary School rallied out front, chanting " Save our school!" Soon, 75 parents and students from TOPS, a K-8 alternative school, marched up chanting to join them.<p>
Students also rose to defend their schools. One group of elementary students sang "This School Is Our School" to the tune of Woody Guthrie's "This Land Is Your Land."<p>
Parents gave statistics and testimonials about the importance of the schools their kids attend, and they also brought up broader issues of race and class to wild cheers from the audience. "The more a school has minority and poor students, the more likely it is to be closed," noted one speaker.<p>
Schools in the South End, which have more students of color and more students on free- or reduced-cost lunch programs, were hit the hardest. Some parents called for preventing all school closures, though most focused on their own individual school.<p>
Last spring, the administration tried to close 10 schools, but parents rose up and succeeded in preventing that. <p>
To win public support for closures this year, the administration appointed a so-called Citizen Advisory Committee. Its mandate was only to select <i>which </i>schools to close, not to decide <i>if </i>schools needed to be closed. So far, the ploy hasn't worked, and parents aren't buying the need to close schools. <p>
The state is underfunding public education by billions of dollars. Washington state is 42nd in per-pupil spending and has class sizes higher than 45 other states. It would need to hire 11,000 teachers to bring class sizes down to the national average. <p>
In 1980, state courts ruled that 50 percent of the state's education budget be devoted to K-12 public education, but this year, only 40 percent of the budget went for that purpose. Seattle's share of this shortfall is $200 million a year. This would cover the school district's budget deficit 10 times over!<p>
Even though the parents are militant and organized, the danger is that if they only focus on individual schools, the administration will play one school off another. A divide-and-conquer strategy could work unless parents organize around saving all the schools--and demanding more funding for education.<p>
A citywide coalition to prevent all closures has been active at all the meetings. This citywide effort needs to be supported and connected with all the protests around individual schools. The parents prevented school closures last year--and they can do it again!<p>

