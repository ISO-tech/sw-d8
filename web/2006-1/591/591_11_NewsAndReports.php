<font face="Times New Roman, Times, serif" size="4"><b>Stand up for immigrant rights</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Loretta Capeheart</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--About 100 people gathered at Union Park May 27 to protest the militarization of the U.S. border with Mexico. The vigil also commemorated the thousands of men, women and children who lost their lives trying to cross the border in search of a better life. <p>
The event was organized by the March 10 coalition, which came together to plan the massive demonstration of more than 300,000 that was the first in the string of large demonstrations for immigrant rights across the U.S. this year. <p>
Attendees were of all ages and included many families. While most participants were Latino, people of many ethnic backgrounds--African American, Asian, Arab and European--joined in. <p>
Those speaking at the front of the rally not only addressed the incredible loss of life inflicted by current immigration laws, but also talked about the threat posed by new laws that would divide families based on the number of years they've lived in the U.S. One young rally attendee put it plainly, stating that the "three-tier system is totally unjust."<p>
A young Muslim woman enrolled at the University of Illinois at Chicago expressed her hope that we can fight for something better, stating that the new laws "are horrible" and that she would rather see "legalization for all" and a "whole different America...a whole different system."<p>
"They use us as low-paid labor," said one longstanding activist who described her disgust with the use of immigrants as scapegoats for economic problems. "We're not going to take it any more."<p>

<font face="Times New Roman, Times, serif" size="4"><b>Protest Laura Bush</b></font><p>
<font face="Times New Roman, Times, serif" size="3">LAURA BUSH has crisscrossed the U.S. leveraging her popularity, which is about twice that of husband George, to raise money for Republican candidates, but in many places, activists have organized to protest her presence.<p>
-- On May 19, 150 activists gathered in <b>Essex, Vt., </b>to confront the First Lady's fundraiser at The Inn at Essex. The event was designed to raise money for congressional candidate Martha Rainville. <p>
Protesters marched along the road leading to the Inn as people on their way to the $5,000 event drove by in BMWs, Ferraris and Hummers. The demonstration was loud and spirited despite heavy rain, and protesters chanted "Laura Bush is high and dry, how many more have to die!" and "George and Laura sitting in D.C., K-I-L-L-I-N-G!"<p>
Just as Bush's convoy arrived and sped past the demonstration, three cops tackled one of the activists, Brendan O'Neill, and threw him to the ground, supposedly because he stepped into the road. The demonstrators responded by chanting, "These cops are mighty low, police brutality's got to go!"<p>
Outrageously, a cop showed up at O'Neill's door the next day to give him a citation for disorderly conduct! <p>
-- In <b>Providence, R.I., </b>about 30 antiwar activists protested Laura's May 19 appearance at a $500-a-plate fundraiser in Providence for Sen. Lincoln Chafee (R-R.I.) at the downtown Biltmore Hotel.<p>
After initially being forced across the street, demonstrators decided to defy police orders and loudly picketed directly in front of the hotel--receiving thumbs-up and signs of support from hotel workers, members of UNITE HERE Local 217. <p>
As stunned Chafee supporters exited the Biltmore directly into the picket line, they were taunted by the chant: "Twenty-five hundred soldiers dead--hope that wasn't one of your kids! A hundred thousand Iraqis dead--let's all go to dinner instead!" The protest was organized by the R.I. Community Coalition for Peace.<p>
We have to stand up against police brutality and continue to build an opposition to the policies of war and oppression exemplified by Laura Bush and the Bush administration.<p>
<i>Dave Buckingham and Shaun Joseph contributed to this report</i>.<p>

