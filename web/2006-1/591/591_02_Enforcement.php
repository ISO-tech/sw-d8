<font face="Times New Roman, Times, serif" size="5"><b>Bush's "voluntary enforcement" fraud</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 2, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE BUSH administration's "voluntary enforcement" programs have failed to get U.S. industry to reduce greenhouse-gas emissions. That's the conclusion of a Government Accountability Office (GAO) report issued last week.<p>
The administration has pushed two programs to encourage U.S. businesses to reduce pollution that contributes to global warming--one administered by the Environmental Protection Agency (EPA), and the other by the Department of Energy. But in both cases, the federal government has done nothing to ensure that companies meet their reduction targets--or even set firm goals.<p>
Last week, Bush's top environmental adviser, James Connaughton, insisted that the government didn't need tougher pollution regulations because administration programs already have the U.S. on track to reduce greenhouse-gas emissions dramatically. <p>
But the GAO report found that U.S. companies pay little attention to "voluntary enforcement." For example, only half of the companies participating in the EPA program had even set reduction goals, according to the report.<p>

