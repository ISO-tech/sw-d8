<font face="Times New Roman, Times, serif" size="5"><b>Delphi puts squeeze on UAW</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 2, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">RISING TENSIONS on the shop floor at Delphi Automotive erupted at the company's Dayton, Ohio, brake plant in May as the bankrupt auto parts maker sought to void its union contracts in federal bankruptcy court.<p>
The battle took place at the Needmore Road brake plant, where a strike a decade ago led to the shutdown of General Motor's (GM) North American production.<p>
On May 5, a supervisor fired Tony Keen, the chair of the shop committee of United Auto Workers (UAW) Local 696, after he told workers to shut down brake cylinder machining equipment that was slippery and unsafe, since management had failed to replace janitors who had retired.<p>
When Local 696 President Joe Buckley arrived to protest Keen's firing, management called police, who handcuffed him and charged him with trespassing. Dan Lamb, a union activist who called a press conference to publicize the incident, was himself suspended a few days later and put on notice--a prelude to termination.<p>
Management eventually backed down, although Keen was kept out of the plant for most of the month, and Lamb was on suspension for a week.<p>
The first night Lamb was back on his job, the supervisor who had targeted him, Mike Weaver, tried to send him out again for failing to meet production standards--but a count of parts showed that Lamb was actually ahead of schedule.<p>
"I think management wanted a war with us and to push around their weight," Lamb told <i>Socialist Worker</i>. "First, they took out our leadership with our elected officials, and then the leadership on the floor--those people who are leading the resistance and the battle of work to rule."<p>
The work-to-rule campaign at Delphi was initiated by Soldiers of Solidarity (SOS), a rank-and-file network formed following Delphi's last filing for bankruptcy.<p>
Since then, an estimated 10,000 of the 34,000 UAW workers at Delphi have accepted a buyout package offered by Delphi and its former owner, GM. Nevertheless, Delphi management keeps pushing its plan to slash wages from about $27 per hour to $16.50--or to as little as $10 if GM doesn't help cover payroll.<p>
If the unions don't agree, management wants approval from a judge to void the contracts altogether. UAW officials, however, didn't hold strike authorization votes until early May, timing that, critics say, enables the union to posture as militant in advance of the UAW convention in June.<p>
In reality, the union has done almost nothing to prepare for a strike, said Lamb--evidence of the UAW's "paralysis" in the face of Delphi's attacks.<p>
Just a few years ago, word that a shop committee chair had been fired would have been enough to trigger a walkout and shut down production. But according to press reports, Local 696 President Buckley ordered workers to stay on the job even as he was being escorted from the plant in handcuffs and charged with trespassing.<p>
The only response was Lamb's press conference, organized, Lamb said, at Buckley's request. "People took this silence as a betrayal," he said. "The shop committee chair is the first person in the plant to be protected" by the union. "[The union local] had a perfect opportunity to take a stand."<p>
The mood on the shop floor is a "dichotomy," he said. "We have people who are ready to fight, to shut down production, and others who are totally scared to death. The others (temporary workers) who were just hired at $14 per hour--those people have a lot of fight, and they came out in droves" to the press conference in the parking lot. <p>
Chuck Layton, who ran on a two-person slate for convention delegate along with Lamb, said that the union isn't keeping up with management's attempts to intimidate workers, such as double-teaming workers with foremen.<p>
As a union activist, Layton said, he's been forced off the more desirable jobs in the plant for allegedly failing to maintain the proper pace. "I am 53, and they put a 25-year-old in my place, and told me, 'You are not going fast enough.'" he said. "They are trying to send me out for two weeks" on suspension.<p>
Since the union did little to keep members informed of the fight to keep Keen's and Lamb's jobs, their return to work hasn't done much to boost workers' confidence in the union, Lamb said. "Your shop committee chair is fired, your president locked out of the plant--I thought that was a given that you walk out," he said. "This is where the UAW has gone."<p>

