<font face="Times New Roman, Times, serif" size="5"><b>A few bad apples?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Nicholas Hart</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 2, 2006 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Kristian Williams, <b><i>Our Enemies in Blue: Police and Power in America.</b></i> Soft Skull Press, 2005, 300 pages, $16.95.</font><p>

<font face="Times New Roman, Times, serif" size="3">WHAT IS the role of the police in society? Are incidents of police brutality the work of a few bad apples? The mission of police is to "protect and serve," but what do they protect and whose interests do they serve? <p>
Kristian Williams' <i>Our Enemies in Blue: Police and Power in America</i> provides an authoritative answer to these and other questions. It's crucial reading for activists or anyone who believes that "Officer Friendly" is the norm and cops like Jon Burge (the former Chicago police commander who tortured confessions out of innocent victims) are aberrations.<p>
Williams traces the origins of the police from its roots in colonial slave patrols, through the rise of political machines (such as New York's infamous Tammany Hall) to the present militarized police force.<p>
The chapter "Cops and Klan, Hand in Hand" describes the historic role police have played in upholding segregation and racism. The KKK generally operated with the full support of police, who were often members. In some instances, whole Klaverns were deputized to terrorize communities and thwart organizing, particularly attempts at Black-white unity and labor organization.<p>
Hostility toward unions was not limited to the South. In early labor struggles, employers would hire armies of thugs to break strikes. This use of private force was widely seen as illegitimate, and increasingly employers turned to the state to provide the forces they needed to break strikes and intimidate or kill labor organizers.<p>
These same skills were used with devastating effectiveness against the social movements of the '60s and '70s, particularly the Black Power movement when the police and FBI employed subversion, violence and murder to isolate and destroy the Black Panthers.<p>
Williams wraps up the book with a survey of community-based approaches to resolving crime used from Northern Ireland to South Africa. Though none of these solutions is perfect, they have significant merits and more legitimacy than the organized violence of the state. Perhaps most importantly they show an alternative to the modern police force.<p>
<i>Our Enemies in Blue</i> paints a vivid picture of a repressive force whose sole purpose is to uphold the status quo, and whose only instrument for achieving this end is violence. One can only draw the conclusion that corrupt, violent cops are not mere bad apples, but rather the expected result of a system organized to reinforce the racism and harsh inequality in our society.<p>
This book shows that the police are one of the greatest obstacles to justice and that we can do without them.<p>

