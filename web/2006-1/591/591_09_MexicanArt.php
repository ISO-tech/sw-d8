<font face="Times New Roman, Times, serif" size="5"><b>A history of Mexico's African descendants</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Helen Redmond</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 2, 2006 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>The African Presence in M&eacute;xico--From Yanga to the Present,</b></i> showing at the Mexican Fine Arts Center Museum in Chicago until September 3.</font><p>

<font face="Times New Roman, Times, serif" size="3"><i>THE AFRICAN Presence in M&eacute;xico--From Yanga to the Present</i> explores racism, integration and struggle through photographs, paintings and other media.<p>
The exhibit is appearing at the Mexican Fine Arts Center Museum in Pilsen--a Mexican neighborhood that is ground zero in the fight for immigrant rights in Chicago--and is based on the groundbreaking work of anthropologist Gonzalo Aguirre Beltr&aacute;n. His phrase "the third root" recognized for the first time the presence of Mexico's African descendants (Afro-Mexican) and their numerous contributions to Mexican culture beginning in 1610. <p>
Colonial M&eacute;xico consisted of three racial groups--indigenous, Spanish and African. African slaves worked in all areas of the economy: as domestic servants and farm hands, on sugar plantations and in silver mines. Blacks added their own cultural, musical and culinary traditions to Mexican society.<p>
Over time, there was a mixing of all three cultures to create a unique <i>mexicalidad</i> ("Mexicaness"). The legacy of racism resulted in this history being either hidden or deliberately ignored.<p>
The exhibition opens with paintings and linocuts depicting Black rebellions. Yanga was a <i>cimarr&oacute;n</i>, a pejorative word for slave. In 1570, he led an uprising and with his followers, fled into the mountains. They lived in freedom for 39 years. The Spanish empire decided that free Blacks were a threat to the institution of slavery, and Yanga had to be crushed.<p>
After a bloody battle and a failed attempt to negotiate his surrender, the Spanish signed a treaty granting freedom to Yanga and his followers. They organized the first free town of Africans in Mexico--San Lorenzo de Los Negros.<p>
Yanga and other slave rebellions are an essential part of Mexican history and deserve to be as well known as the liberation movement led by Emiliano Zapata.<p>
Another section of the exhibition explores the role of Afro-Mexicans and the revolution of 1910. Zapata's forces made alliances with an all-female battalion of Afro-Mexicans from Guerrero.<p>
Unfortunately, after the revolution, the government continued to erase evidence of the African presence in Mexico. In 1992, the Mexican government officially recognized Mexico's African roots.<p>
This is a powerful art show that takes up tough questions that contemporary society is still struggling with--racism, civil rights, freedom. Don't miss it.<p>


