<font face="Times New Roman, Times, serif" size="5"><b>Railroaded onto Texas death row</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Robert McDonald</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 7, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">WHEN 19-year-old Stacy Stites was found murdered on April 23, 1996, the small central Texas town of Bastrop was shaken to its core. <p>
Now, almost a decade later, Bastrop is shaken again by evidence suggesting that the person convicted of her murder, Rodney Reed, is an innocent man railroaded to death row.<p>
Reed finally got a new hearing, ordered by the Texas Court of Criminal Appeals, before a Bastrop County judge into allegations of prosecutorial misconduct, suppressed evidence and contrary eyewitness reports that have simmered away for years. <p>
Reed's family and supporters hope last month's hearing will lead to a new trial and Rodney's freedom. But they fear they won't get justice from Judge Reva Townslee-Corbett--who is to rule on charges of flaws in a trial that her own father presided over.<p>
During the hearing, Reed's appellate defense team argued that crucial evidence which could have shown Rodney's innocence and altered the course of the trial was withheld by prosecutors during the original 1998 trial.<p>
One witness told the hearing that Jimmy Fennell, Stites' fianc&eacute; and a local police officer, was seen arguing with Stites in the early-morning hours before she was found dead. Another testified that, while attending a police training session with Fennell, she heard him say that if he found out his girlfriend was cheating on him, he would strangle her with a belt, so as not to leave fingerprints--exactly how Stites was murdered.<p>
Reed's lawyers also reiterated other holes and inconsistencies in the case against Rodney:<p>
-- The only physical evidence linking Reed to the crime was a semen sample taken from Stites. But Reed has explained that he was having an affair with Stites--which is also a motive for Fennell to kill her.<p>
-- Photos taken at the medical examiner's office show burns and injuries that weren't in images from the crime scene itself.<p>
-- A May 13, 1998, Department of Public Service report states that fresh beer cans found at the crime scene contained DNA from Stacy Stites and police officers David Hall and Ed Salmela (the original investigator for the case).<p>
-- The truck apparently used to transport Stites' body, which contained fingerprints from only Jimmy Fennell and Stacy Stites, was handed over to Fennell the day that it was discovered. Fennell sold the truck the next day<p>
-- In a polygraph test, Fennell failed the question: "Did you strangle Stacy Stites?" <p>
Though these issues came out in the hearing, Rodney's supporters were unhappy with the proceedings. They say that Judge Reva Townslee-Corbett, whose father presided at Rodney's original trial, clearly favored the prosecution during the hearing and treated the defense team with distain.<p>
For example, she allowed the prosecution to call as a witness an investigator who claimed to have learned that no other cadet in the training session with Fennell said they heard his comment about strangling a victim with a belt--a clear violation of courtroom procedure, say Reed's lawyers.<p>
"She's guarding her father's reputation," said Cassandra Jones, a Bastrop resident. "It's a conflict of interest."<p>
Sandra Reed, Rodney's mother and a member of the Campaign to End the Death Penalty, said she was "appalled" by Townslee-Corbett's behavior, but nevertheless hopes "she'll do the right thing."<p>
Townslee-Corbett is expected to make a ruling by this summer. But Rodney's supporters say that even if she doesn't rule in his favor, the critical pieces of evidence are now on the record for the Court of Criminal Appeals to review.<p>
On March 25, the Campaign to End the Death Penalty organized a march to the Bastrop County Courthouse. Some 75 people took part, chanting "Let the new evidence show Rodney's innocence." That night, a documentary, <i>State vs. Reed, </i>which shows the many discrepancies in the case, was shown to around 100 people at the local Boys and Girls Club.<p>
Rodney's supporters now plan to take a petition calling for a new trial and signed by hundreds to the Court of Criminal Appeals. As Sandra Reed put it, "Whatever it takes, I'm going to do it."<p>

