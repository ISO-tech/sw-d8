<font face="Times New Roman, Times, serif" size="5"><b>The struggle for justice in Argentina</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 7, 2006 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">A PROTEST of 150,000 people filled the Plaza de Mayo and surrounding streets of Buenos Aires, Argentina, March 24 to mark the 30-year anniversary of the coup that brought about nearly 10 years of brutal military dictatorship.<p>
From March 24, 1976 to 1983, the people of Argentina were subject to severe repression, persecution and violence, including the abduction of over 30,000 people, known as the "desaparecidos." Every year on this anniversary, people take to the streets to demand justice for the crimes committed under the dictatorship and to speak out against the continuation of repression and injustice.<p>
This year, President Nestor Kirchner established a national holiday to "remember" the dictatorship. He called for an event to celebrate the triumph of human rights under his own administration with the compliance of some of the pro-government human rights organizations, including the Madres de la Plaza de Mayo. <p>
However, the reality behind the rhetoric is the need to calm the boiling political crisis that the government has been confronted with since the economic crisis of 2001, when then-President De la R&uacute;a was ousted by mass protests.<p>
The stability and legitimacy of Kirchner's presidency has been eroded since he took office in 2003. The corruption of top government officials has surfaced and caused public outrage. The largest case was the recent dismissal of the mayor of Buenos Aires, An&iacute;bal Ibarra, for his role in the official neglect of safety regulations that led to the deaths of 193 young people in the Croma&ntilde;&oacute;n nightclub fire a year ago. <p>
This was closely followed by the resignation of Sergio Acevedo, the governor of the Santa Cruz province, to quell the growing discontent over the government's use of harsh police repression in attempt to put down a successful oil workers' strike in the city of Las Heras. <p>
The "petroleros" have already won higher wages for the entire province and a raise in the minimum taxable income, but police forces still occupy Las Heras and nearly 20 union leaders and activists remain imprisoned.<p>
Kirchner exposed the real priorities of the neoliberal government he heads when he paid the International Monetary Fund (IMF) debt this year. Hunger, unemployment and poverty have all risen dramatically since the end of the dictatorship. Kirchner is no exception in his willingness to concede to imperialism at the expense of the Argentine people.<p>
Despite his attempts to co-opt the protest, Kirchner was confronted with a document from the more than 300 groups that organized the demonstration that demanded: Trial, punishment and imprisonment for all those responsible for the genocide; restore the identities of the hundreds of disappeared youths; end the repression, withdrawal of the police from Las Heras and all social conflicts, and liberate political prisoners; enough with the policies that cause hunger, unemployment and poverty; no to the payment of the external debt, no to the free trade agreements; U.S. out of Iraq and Latin America and immediate withdrawal of Argentine troops from Haiti.<p>
The protest's message reflects the emergence anti-imperialism seen all over Latin America. The injustices of imperialism today will not be ignored and they will not continue without opposition.<br>
<b>Jocelyn Blake,</b> Buenos Aires<p>

