<font face="Times New Roman, Times, serif" size="5"><b>Britain hit by one-day strike over pensions</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 7, 2006 | Page 5</font><p>

<font face="Times New Roman, Times, serif" size="3">MORE THAN 1 million workers went on a one-day strike across Britain March 28 to protest planned cuts in government pensions. <p>
The walkout of public-sector workers closed down many schools and libraries, halted some garbage collection and public transportation, and shut down some courts and other government offices across parts of England, Northern Ireland, Wales and Scotland. At least 17,500 schools were closed--including 70 percent of schools in London.<p>
In all, 11 unions--including several of Britain's largest--participated in the strike, drawing out as many as 1.5 million workers in the largest strike in Britain since the 1926 general strike.<p>
The strike was called as a response to government plans to change the "rule of 85," which allows some local government workers to retire at a full pension as early as age 60, as long as their age and years of service add up to 85.<p>
Prime Minister Tony Blair's New Labour government claims that it can't afford rising pension costs and wants to scrap the rule for teaching assistants, school-meal workers, charity collectors, garbage collectors and other local government employees beginning in the fall.<p>
Unions point out that changing the law will hit workers in lower-paying jobs the hardest. Better paid public-sector workers, including uniformed police and national health service workers, will still be allowed to retire early with no penalty. <br>
According to Unison, Britain's largest public-sector union, nearly three-quarters of those affected under the new rules are women, most of whom work part-time--and the loss of up to one-quarter of their pension for early retirement will be devastating.<p>
"Strike action is the only option left to local government workers to demonstrate the burning resentment and anger they feel over the government and employers taking away their pension rights, when those same rights have been given to every other public-sector pension scheme," Unison general secretary Dave Prentis told the BBC.<p>
Walking the picket line at Merseytravel, the agency that runs public transportation in the area around the northern city of Liverpool, 61-year-old Karl Crighton told Britain's <i>Guardian,</i> "The government's pension plans won't affect me because I'm too old, but I'm here for the younger people. I don't think the public realize that a lot of our employees are low-paid workers and are not going to get much when they retire as it is. Being here is the right decision."<p>
Predictably, the Confederation of British Industry, the UK's largest business lobby, labeled strikers "selfish" and "divisive."<p>
But the strike action is a long time in coming. In fact, union leaders called off a planned one-day strike ahead of last year's May elections, after the Blair government offered to "negotiate" pension changes.<p>
And while the unions are fighting for current employees, it appears as though they may be willing to take a two-tier deal, accepting the pension cuts for new hires.<p>
Even so, the strike is a black eye for Blair and his increasingly troubled government. The media are filled with speculation that Blair--increasingly unpopular because of his role as chief cheerleader for the U.S.-led war on Iraq--won't remain in office for much longer. The pension cuts, along with further plans for increased privatization and more than 100,000 public sector job cuts by 2008, are making matters worse.<p>
Public sector unions have scheduled three more days of regional strike action later this month, and a possible two-day strike on May 3 and 4, in the run-up to local government elections.<p>

