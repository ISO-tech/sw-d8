<font face="Times New Roman, Times, serif" size="4">What's missing from the debate about immigration</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A call for open borders</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 7, 2006 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">CONGRATULATIONS TO all of those who have walked out in support of the Mexican Americans who are seeking the freedom to travel across the border. Bienvenidos. Never allow anyone to tell you that you are an "illegal" or an "alien." We are all part of the human family. You are our brothers and sisters. We welcome you. <p>
Listening to corporate media and talk shows has exposed the dark underside of the U.S. culture. Hate-filled, xenophobic speech is filling the airways. Fox News seems to be in a state of panic. Some in the U.S. want Mexicans to be treated as second-class citizens. Might as well paint a black dot on the forehead of every immigrant. <p>
Our paranoid, racist policy toward Mexicans is apparent to the whole world. Mexicans should be given at least the same rights as those coming from Cuba. Our borders should be wide open. Welcome stations should be built there to assist the travelers. <p>
One argument being used against immigration is that the inclusion of Mexican workers will lower wages in the U.S. That is like blaming the residents of the Ninth Ward for the failure of the levee system in New Orleans. <p>
The purchasing power of wages in the United States has been steadily falling for decades. It has nothing to do with Mexicans. It has everything to do with the globalization of corporations. Anger should be directed at corporate CEOs and shareholders, not the workers. <p>
The fight for a fair wage system must include efforts to globalize the international labor market. To do otherwise ignores the basic facts. The work force needs global standards for wages, benefits, safety and working conditions. <p>
Opening the borders would bring many advantages. Workers coming from Mexico and other countries would boost the Social Security system by increasing the number of workers. In some U.S. communities the population is decreasing. School systems are experiencing decreasing numbers of students. The new residents would be welcomed in those communities. <p>
There also would be cultural benefits. The U.S. population is deficient in language skills compared to other industrialized nations. This influx from the global South could help the U.S. become bilingual. Many in other countries speak three or four languages. <p>
The history of the southern border is relevant today. How did the U.S. acquire that large piece of real estate known as Texas? How much Mexican blood was shed there? We have a debt to pay to our southern neighbors. The least that we should do is either allow Mexicans to live and work here permanently, or else return Texas to its original owners.<br>
<b>Rosemarie Jackowski,</b> Bennington, Vt.<p>

