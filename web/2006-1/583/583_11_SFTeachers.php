<font face="Times New Roman, Times, serif" size="4">District demands for pay cut bring talks to standstill</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>SF teachers okay strike</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Adrienne Johnstone, United Educators San Francisco</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 7, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Educators voted overwhelmingly March 29 in favor of authorizing a strike if mediation fails to bring an acceptable settlement for our membership. <p>
Following a unanimous decision in the union's General Assembly to set a strike vote, the members of United Educators San Francisco (UESF) voted 2203 to 317 in favor of strike authorization. UESF members have worked nearly two years without a contract and haven't seen a raise in nearly four years. <p>
The backdrop to this fight has been a year of little to no movement in negotiations and, following the declaration of impasse, even less progress in mediation. <p>
The biggest step forward for UESF members came in November, after our sisters and brothers in Service Employees International Union (SEIU) Local 790 school division won contract gains based largely on overwhelming solidarity from UESF. An SEIU strike was called off at the eleventh hour when it became clear that the majority of UESF members were planning to honor SEIU picket lines. <p>
After SEIU called off their strike, the school district offered UESF members a 7.5 percent raise, up from 2 percent. While district propaganda claims that this is a generous offer and that our union leadership should allow us to vote on the proposal, the district underestimates the increasing anger and willingness to act among San Francisco teachers. <p>
The stakes in this struggle were recently raised when the union found out that, at the end of 2006, the city of San Francisco will have received enough money from the state of California to fund a cumulative cost-of-living allowance (COLA) increase of 17.5 percent. Instead of giving this money to the people who do the work in the schools, the district is only offering a 7.5 percent pay increase spread over two years and an insulting 2 percent retroactive raise that only goes back to January 1 of this year. <p>
Because the cost of living in San Francisco has risen 7.9 percent since our last pay raise, the district is still asking us to settle for what amounts to a pay cut. <p>
In a recent memo, school district officials tried to use recent school closings against the union, claiming, "These decisions [to close schools] have made possible the compensation increases currently offered to teachers and paraprofessionals." <p>
This is an outright lie and contradicts the districts' own claim at the time of the closings that they were made inevitable by falling student enrollment. "[The district] is always going to try and give you the minimum, if anything," said Derrlyn Tom, a UESF Executive Board member from Mission High School. "We didn't think we'd have to be really aggressive but that was a mistake." <p>
Tom believes that we need to begin rapid preparations for a possible strike. Regardless of what happens, "it will give us a sense of what we're made of," she said in an interview. <p>
UESF activists spent recent weeks visiting school sites and working phone banks to get the membership out to the vote. The school district prepared for the strike vote by stuffing intimidating memos into every single school worker's mailbox, but their intimidation fell on deaf ears. <p>
The UESF rank and file will have to figure out how to pressure union leaders to move forward swiftly. Picket captain training has already been scheduled, and many teachers are talking about possibly striking during the detested week of standardized testing in late April. <p>
"The district doesn't give a damn if we go into another summer with no contract," said Joelle Tidwell, a first grade teacher and union activist at Carver Elementary. "We have to be serious about striking. I think that's what it's going to take."<p>

<font face="Times New Roman, Times, serif" size="5"><b>Oakland teachers set April 20 strike date</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jessie Muldoon, Oakland Education Association</b></font><p>

<font face="Times New Roman, Times, serif" size="3">OAKLAND, Calif.--After nearly two years without a contract, Oakland teachers voted to authorize a limited strike to put pressure on the Oakland Unified School District to take contract talks seriously. Due to stalled negotiations, workers set an April 20 strike date.<p>
Sticking points are art and music enrichment in elementary schools, health care costs and district demands for restoration of a 4 percent pay cut. The school district is intent on containing rising health care costs by capping its contribution to the HMOs that provide health care. This would be tantamount to an ongoing pay cut for teachers.<p>
In a period when many unions are settling for concessionary contracts, the Oakland Education Association (OEA) is drawing a line in the sand. If the OEA and San Francisco teachers strike together, the impact could be enormous.<p>

