<font face="Times New Roman, Times, serif" size="5"><b>Death row talks back to Etan Thomas</b></font><br>
<font face="Arial, Helvetica, sans-serif" size="2">April 7, 2006 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">DAVE ZIRIN is the author of <i>What's My Name Fool?: Sports and Resistance in the United States </i>(Haymarket Books). He is a regular writer for the <i>Nation </i>and a columnist for <i>Slam Magazin</i>e. His columns can be read at <a href="http://www.edgeofsports.com" target="_blank">www.edgeofsports.com</a>. This column appeared before an event in San Francisco on March 26 called "Civil Rights Slam for Justice" where Washington Wizards basketball player Etan Thomas was a featured speaker.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">THIS SUNDAY at 4 p.m., I am proud to be speaking at an event in San Francisco called a "Civil Rights Slam for Justice," sponsored by, among others, the Campaign to End the Death Penalty (CEDP).<p>
The slam will be at the Malcolm X school at 350 Harbor Street. In addition to myself and a crew of young artists, activists and poets, speakers will include an NBA basketball player by the name of Etan Thomas.<p>
Regular readers of my column know that I'm not exactly shy about singing the praises of the Washington Wizards forward. Etan plays a gritty, elbows-up style of basketball, but on a microphone he is pure Jordan. In the tradition of Amiri Baraka, his poems are sharp enough to cut glass, and generous enough to leave seedlings that can sprout in the cracks. <p>
I first heard about Etan's political poetry when a rumor started going around Washington, D.C., that this rather gigantic gentleman with dreads was going to U Street coffee houses reading anti-death penalty, antiracist verse in front of a crowd you could fit in a van.<p>
Since then, Etan has risen to every occasion, speaking out at last September's antiwar rally, speaking out against the mistreatment of Katrina refugees, speaking out against the execution of Stan Tookie Williams, and speaking out through a published book of verse fittingly enough called <i>More Than an Athlete</i> (Moore Black Press).<p>
Right here, for the first time, death row speaks back to Etan Thomas. This comes in the form of a letter from Illinois death row prisoner Stanley Howard. Stanley, always organizing, typed his message to Etan on the back of a fact sheet that explains his case.<p>
Here, his letter is republished with permission from both Stanley and Etan. I pass on Stanley Howard's letter so folks can see that athletes don't take political stands for their own amusement or ego, but to be part of something larger than themselves. I also pass it on to demonstrate how a prisoner on death row has as much a capacity to inspire as any jock.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear Brother Etan Thomas</b>:<br>
My name is Stanley Howard, and I'm currently incarcerated at the world famous Stateville Correctional Center/Warehouse in Joliet, Illinois. I'm a 43-year-old Black poor man from Chicago who has spent the last 22 years kidnapped by this unmerciful system--16 of those years were spent trying to stop the state of Illinois from lynching me on death row.<p>
I'm no longer suffering on death row (fighting yet another wrongful conviction), but my heart is still in the struggle to end the death penalty because I can still hear the cries for justice and understanding loud and clear in my ears.<p>
I've recently heard about your upcoming scheduled appearance at a Campaign to End the Death Penalty (CEDP) event, and I just wanted to send these words of thanks to show my sincere appreciation.<p>
I've heard so much about your activism against classism, racism and this unjust system and government, and you'll be surprised to know that you're a great inspiration to many of the guys behind this 30-foot wall. Because like the title of your book says, you're "more than an athlete."<p>
I was on death row when it seemed like nobody cared what happened to death row prisoners, and worthless politicians were climbing on top of each other to pass laws and rules designed to make it easier to be sent to death row; harder to get off; and, faster to execute. They caused hundreds to be executed during this time period trying to prove they were not soft on crime.<p>
They were able to kill all these people (some of whom had to be innocent, like me), even though we had many well-established groups and organizations fighting to abolish the death penalty.<p>
Everything began to change with the bold and aggressive grassroots efforts of the CEDP, because they consist of everyday people who are not sitting behind desks pushing paper, but out on the streets organizing, educating, protesting and agitating the so-called Powers That Be.<p>
Everyone on death row loves the CEDP, because they changed the face of how this life-saving movement is fought--helping to put the death penalty under the national spotlight; obtaining a death penalty moratorium; highlighting many cases; and, convincing Gov. George Ryan to empty out Illinois' death row and granting my request for a pardon and three other pardons.<p>
So on behalf of all the brothers and sisters still fighting to stop from being lynched on death rows around the country, I thank you for joining the struggle and helping to bring this madness to an end. THEY SAY DEATH ROW--WE SAY HELL NO!!! Thank you for being "more than an athlete"!!!<br>
<b>Stanley J. Howard, </b>Reg. # N-71620<br>
Stateville Correctional Center<br>
Route 53, P.O. Box 112<br>
Joliet, IL 60434<p>

