<font face="Times New Roman, Times, serif" size="5"><b>Sikorsky strike ends with bad deal for union</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jay Poppa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 7, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">STRATFORD, Conn.--After nearly six weeks on strike, 3,600 workers from Teamsters Local 1150 at Sikorsky Aircraft voted by a narrow margin of 75 votes to accept the company's contract proposal.<p>
The ratified contract includes the increased health insurance costs originally proposed by Sikorsky that sparked the February 20 strike at several facilities in Connecticut and Florida. The contract will double worker co-pays the first year and increase health costs by 15 percent during the next two years of the contract. These expenses will eat up annual wage 3.5 percent wage increases during the three-year contract, meaning that workers will end up with a pay cut.<p>
One of the only changes to the contract is a $1,000 signing bonus that the company would match if put toward worker retirement plans.<p>
The strike reflected the strengths and weaknesses of the labor movement elsewhere around the country. <p>
The solidarity and support from other unions that climaxed in an early March rally of close to 5,000 union members, supporters and community members showed that workers at Sikorsky were not isolated in their struggle, but were part of a fight for better health care for all workers. <p>
The wide array of supporters--from local unions to the statewide antiwar coalition--also identified the strike as part of the larger struggle against profitable corporations demanding concessions from their workforce.<p>
The determination and solidarity to walk out with limited strike pay for close to six weeks was also a major strength. Throughout the strike, only a small percentage of workers crossed the picket line, causing delays in producing helicopters bound for deployment in Iraq and Afghanistan.<p>
What led to the acceptance of the company's virtually unchanged contract was the strategy of national and local Teamster officials, who sought the support of Connecticut's congressional delegation as a way to pressure Sikorsky to settle. <p>
This focus took power out of the hands of workers and made picket lines into symbolic protest scenes that were unable to keep the nonunion salaried and clerical staff out of the company's main facility in Stratford. The "wait and see, one day longer, one day stronger" strategy of union officials locked the workers in a battle of attrition from the outset. <p>
Throughout the strike, there was the sentiment among some workers to step up the militancy of the picket lines. Ideas floated during informal conversations ranged from holding more large rallies on a regular basis, to using the Teamsters' 18-wheelers to block the plant gates, to occupying the main facility in Stratford. <p>
In the end, however, these conversations were just that--without rank-and-file leadership to put forward an alternative strategy. In coming years, workers can continue these discussions while building the organization needed to force concessions out of Sikorsky.<p>

