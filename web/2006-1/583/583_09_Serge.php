<font face="Times New Roman, Times, serif" size="5"><b>State repression: A guide for activists</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 7, 2006 | Page 9</font><p>

<b>Victor Serge, <b><i>What Every Radical Should Know about State Repression: A Guide for Activists. </b></i>Ocean Press, 160 pages, $14.95.</b><p>

<font face="Times New Roman, Times, serif" size="3">IN RECENT months, government spying has hit the news, with revelations of the Bush administration's secret, illegal wiretappings of U.S. citizens and a secret Pentagon database detailing possible "threats to national security"--including peaceful antiwar protests.<p>
In light of these developments, the newly reissued edition of Victor Serge's <i>What Every Radical Should Know about State Repression </i>makes a welcome--and relevant--addition to any activist's bookshelf.<p>
Originally an anarchist, Serge joined the Russian Communist Party on arriving in Petrograd in February 1919 and worked as a journalist, translator and editor for the Communist International. In the early 1920s, he exposed the files of the Okhrana (the "Defensive")--the Tsarist secret police. First published in its entirety in France in 1926, Serge's handbook on repression aimed to educate and inform political activists.<p>
As he makes clear, repression is a feature of every modern state attempting to preserve an order mired in inequality. Serge details the methods of the Okhrana--including government snooping through mail and tapping of telephone lines, and chillingly, the extensive organization of paid agents and provocateurs to infiltrate and inform on political organizations.<p>
At heart, Serge's message is not about frightening activists into paranoia, a retreat from politics, or an abandonment of mass political action in favor of political adventurism that could be carried out on an individual basis. <p>
Instead, he stresses that while all capitalist states use repression, particularly in times of the growth of a radical mass movement, repression alone cannot stamp out a movement that is rooted in the working class--and that cultivates leadership among that class.<p>
According to Serge, the initial success of the Russian Revolution, despite the worst abuses of the state, was owed to the fact that political organization had been built that embodied and represented the class consciousness of the majority of society. While police may have success in arresting this or that activist, or even shutting down individual political organizations, a principled movement that cultivates leadership in and of the working class can withstand the worst attacks.<p>
"Repression can only really live off fear," counsels Serge. "But is fear enough to remove need, thirsts for justice, intelligence, reason, idealism--all those revolutionary forces that express the formidable, profound impulse of the economic factors of a revolution? Relying on intimidation, the reactionaries forget that they will cause more indignation, more hatred, more thirst for martyrdom, than real fear. They only intimidate the weak; they exasperate the best forces and temper the resolution of the strongest."<p>
Serge also relates how, as the revolution broke out, police fled and the worst Okhrana officials were brought to justice. "[O]ne day...we set up in a kind of museum a number of particularly interesting items taken from the secret archives of the police of the Empire," he writes, adding, "Our exhibition took place in one of the finest halls of the Winter Palace."<p>
Perhaps, one day, today's radicals will be able to walk the halls of the White House examining the secret archives of the NSA and FBI--while rejecting the idea that rounding up Arabs and Muslims or infiltrating peace groups could ever have happened in a society calling itself a democracy.<p>

