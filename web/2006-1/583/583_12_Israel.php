<font face="Times New Roman, Times, serif" size="5"><b>Israeli offensive ignored by media</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 7, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">ISRAEL LAUNCHED a massive artillery strike on northern Gaza on April 1, pounding the area with air strikes and missiles launched from gunboats.<p>
The assault lasted some 48 hours and came one day after the U.S. announced it was cutting off contact with the newly sworn-in Hamas government, but would maintain contact with Palestinian Authority (PA) President Mahmoud Abbas. The pretext for the attack was the firing of a half-dozen Qassem rockets into Israeli territory. The rockets landed in the Negev desert and caused no damage or injuries.<p>
"Gaza has a new government, and we expect it to enforce quiet," said an Israeli military officer. "If the rocket fire persists, severe punitive measures will be taken." This could include launching missiles at residential areas, causing Palestinians to flee their homes, according to Israeli officials.<p>
The Gaza offensive was met with a deafening silence. The mainstream media around the world ignored this latest round of Israeli terror--as if Palestinians should expect this kind of aggression now that Hamas has assumed leadership of the PA.<p>
Dozens of Israeli shells hit agricultural land near the towns of Jabaliya, Beit Lahya and Beit Hanoon. Ominously, these are the same areas that Israel planned to target in the second part of a three-stage offensive to punish any Palestinian actions considered "threatening" by Israel, according to a December report in Israel's <i>Yedioth Ahronoth </i>newspaper. The third stage would include a massive ground operation to reoccupy Gaza.<p>
According to an unnamed Israeli military source, a similar plan to crack down on Palestinian resistance in the West Bank is being considered by senior figures in Kadima, the party that won Israel's March 28 elections and is now in the process of forming a government. "The military aim will be to seize the main Palestinian towns in the West Bank and continue the operation until the last of the militants are dead or under arrest," said the official.<p>

