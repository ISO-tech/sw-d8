<font face="Times New Roman, Times, serif" size="5"><b>ExxonMobil and its shady dealings</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 7, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">DID OIL giant ExxonMobil pull strings to get the Internal Revenue Service (IRS) to audit the environmental group Greenpeace?<p>
According to recent reports, more than two years ago, Public Interest Watch (PIW)--a supposed "watchdog" of nonprofit groups--wrote to the IRS to accuse Greenpeace of "money laundering," and to urge that the group be audited. The IRS eventually agreed, beginning a three month-long review of the organization's finances last year.<p>
Far from being an impartial "watchdog," however, PIW has received almost all of its funding from ExxonMobil--a frequent target of Greenpeace protests and campaigns.<p>
Shady deals like this are just one reason why prominent activists--including historian Howard Zinn and antiwar activist Cindy Sheehan--have signed on to a call for a boycott of ExxonMobil and other firms related to the company through its board of directors.<p>
The call for the boycott, launched in late December by the group Consumers for Peace, points out that ExxonMobil, the world's most profitable oil company, has deep ties to the Bush administration.<p>
A two-week march to George Bush's Crawford, Texas, ranch kicked off from ExxonMobil headquarters in Irving, Texas on April 1. The call for the march includes not only an end to the war and immediate withdrawal of U.S. troops, but for ExxonMobil to spend $7 billion of its record $36 billion profits from last year to alleviate war suffering and compensate people who have been harmed by its operations.<p>
"ExxonMobil has made at least $7 billion extra in 2005 because of the invasion and occupation of Iraq," Nick Mottern, director of ConsumersforPeace.org, said in a statement. "This is unearned money, taken from consumers, and it needs to be returned to society. "We propose that ExxonMobil write checks to private organizations for relief in Iraq, for war-related injuries of U.S. veterans and to compensate people in the U.S. and elsewhere who have been harmed by ExxonMobil operations." <p>

