<font face="Times New Roman, Times, serif" size="5"><b>Army's tab for wounded soldiers</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 10, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE LAST time that First Lt. William "Eddie" Rebrook saw his body armor, it was being pulled off of his bleeding body while he was being evacuated by helicopter following a battle in Najaf in 2004. Rebrook was forced to leave the Army because of wounds to his arm.<p>
But before he could be discharged, the Army wanted $632 from him--for missing gear, including the blood-soaked Kevlar vest that medics had lifted off his body and which the Army later destroyed as a biohazard.<p>
According to Rebrook, when he attempted to get a battalion commander sign a waiver for the Kevlar vest, he was told he would have to supply statements from witnesses to verify the body armor was taken from him and burned. <p>
Rebrook was forced to borrow money from friends to pay the Army before he could be discharged in early February--or face the prospect of not being discharged for months. "My son loved the Army and was proud of serving his country," Beckie Drumheler, Rebrook's mother, told the <i>Charleston Gazette</i>. "For any soldier to be treated like this is outrageous."<p>
When Rebrook's story broke last month, the military quickly refunded his money--calling it a clerical error. But Rebrook isn't the only wounded soldier to be told that he owes money to the Army.<p>
Staff Sgt. Eugene Simpson, who was paralyzed when a roadside bomb severed his spine, had his pay discontinued while he was still in the hospital. Simpson's wife called him one day to tell him that his check had suddenly stopped coming, and that she was having problems making ends meet for her and their four children.<p>
"I was like, I don't know what to do. You know, I'm still in the hospital. I can't actually get up and go around and talk to these different people," Simpson told ABC's <i>Nightline</i>.<p>
And when he did try, he got the runaround. "Every day is something different," he said. "Well, this person isn't in. I'll have them call you back, give it a couple days. Couple days go by, I call back, well I got somebody else for you to talk to. And days lead to weeks, and weeks lead to months."<p>
It wasn't until <i>Nightline </i>inquired at the Pentagon that Simpson was finally told the Army had mistakenly continued to pay him a combat duty bonus while he was in the hospital. He had been overpaid thousands of dollars, and the Army wanted its money back. "By law, he's not entitled to the money," explained Col. Richard Shrank, "so he must pay it back."<p>
With no warning, the Army withheld the paralyzed soldier's pay for four months until it got back the amount he owed.<p>
The Army admitted to <i>Nightline</i> that 5,549 soldiers--about one out of five soldiers removed from battle for medical reasons--later had payroll problems. But a study conducted by Capt. Michael Hurst, formerly of the First Infantry Division, estimates that eight out of 10 of its wounded soldiers from Iraq have gone through the same or a similar ordeal.<p>
As Hurst told <i>Nightline</i>, "[F]or a lot of these soldiers, this is just a betrayal really. They feel abandoned, when they're in such a vulnerable position and their leaders aren't taking care of them."<p>

