<font face="Times New Roman, Times, serif" size="5"><b>Book targets union corruption...and reform?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Joe Allen, Teamsters Local 705</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 10, 2006 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Robert Fitch, <b><i>Solidarity for Sale: How Corruption Destroyed the Labor Movement and Undermined America's Promise. </b></i>PublicAffairs, 2006, 560 pages, $28.50.</font><p>

<font face="Times New Roman, Times, serif" size="3">THE 1991 election of Ron Carey as the first reform leader of the Teamsters was a bombshell that hit the labor movement and Corporate America.<p>
Corrupt and complacent trade union officers were put on notice their members wanted a different direction for their unions, while bosses used to big concessions had to face a revived Teamsters union. What made his victory even more stunning was the role played by the longstanding reform group Teamsters for a Democratic Union (TDU).<p>
The high point of the Carey administration was the 1997 strike against UPS. Labor historian Nelson Lichtenstein wrote that the strike ended "the PATCO syndrome, a 16-year period in which a strike was synonymous with defeat and demoralization."<p>
But for writer Robert Fitch, this was all a "glittering mirage." His book <i>Solidarity for Sale: How Corruption Destroyed the Labor Movement and Undermined America's Promise</i>--a book ostensibly about union corruption--rehashes the worst smears and lies about Carey that came from the mobbed-up, old guard of the Teamsters.<p>
Carey, for Fitch, was just another corrupt Teamster officer in the mold of his predecessors. Fitch also dismisses the idea that Carey was banished from the union for leading the UPS strike as "paranoid," "self-serving" and "politically na&iuml;ve."<p>
While there many things wrong with Fitch's book, this review will focus on the parts that apply to the Teamsters.<p>
First, the charges made in it against Carey have been investigated and dismissed long ago. In February 1996, retired <i>New York Times </i>labor reporter William Serrin wrote an article called "Hacks and Hatchet Jobs," which addressed many of these issues and traced them back to their source.<p>
Serrin's article appeared in the early stages of a bruising re-election campaign pitting incumbent reformer Carey against the well-financed and mobbed-up challenger James P. Hoffa. Among the more fantastical smears published by such venerable institutions as <i>Time </i>magazine, the <i>New York Times</i> and National Public Radio--and rehashed by Fitch--was that Carey was an agent of the Luchese crime family and personally controlled million of dollars of UPS stock through his father.<p>
Serrin showed that these false stories emanated from the Hoffa campaign consultants, particularly, from Richard Leebove and George Geller, former associates of right-wing cult figure Lyndon LaRouche.<p>
These charges against Carey were thoroughly investigated and dismissed by the Department of Labor and the Independent Review Board (IRB), the disciplinary body created by the settlement of the Justice Department's RICO suite against the Teamsters. and, in Serrin's words, they received "little attention" from the press.<p>
Carey defeated Hoffa and went on to lead the 1997 strike against UPS, which among other things won the creation of over 10,000 new full-time jobs. Soon after, however, Carey was banned from the union, by the same IRB that exonerated him a few years earlier.<p>
Is it "paranoid" to the think that Carey was banished from the union because he led a national strike at UPS? Absolutely not.<p>
The Republican-controlled Congress led by right-wing nut Rep. Pete Hoekstra orchestrated a witch-hunt against Carey that put enormous pressure on the IRB and the Justice Department to void the 1996 election and "neutralize" Carey--which they did.<p>
Hoffa rode this witch-hunt to power in a "special election" in 1998 against reformer Tom Leedham, a former member of the Carey slate and Teamster warehouse director. Hoffa couldn't win an election on his own; he needed the help of the federal government.<p>
Three years later in 2001, Carey was indicted by the Justice Department on the very same charges that led to his banning from the union by the IRB and was acquitted of all charges that year. Another veteran labor reporter, <i>Newsday's </i>Ken Crowe, described the verdict as a "vindication" of Carey. <p>
All of this, however, isn't good enough for Fitch. Fitch's book is not just bad history--it's irresponsible, for someone who claims they want a better U.S. labor movement.<p>

