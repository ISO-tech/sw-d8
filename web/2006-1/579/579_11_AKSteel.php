<font face="Times New Roman, Times, serif" size="5"><b>AK Steel locks out union</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Vincent Beach, Shane Johnson and Steve Succop</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 10, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">MIDDLETOWN, Ohio--Proposing drastic increases in health care costs and a freeze of retirement pensions, AK Steel locked out 2,700 union workers February 28. The workers are members of the Armco Employees Independent Federation (AEIF), a name that reflects the plant's former owner. <p>
One week before the contract deadline, union members voted by 98 percent to authorize a strike by the workers, who include riggers, crane operators, maintenance personnel and office workers. In its 68-year history, AEIF has never gone on strike, but this is the second time 20 years that the union has been locked out.<p>
AK has the resources to negotiate a fair contract. Numbering 376 on the <i>Forbes </i>500 list and the fifth largest steel company in the U.S., the company reported earnings of $5.6 billion for fiscal year 2005. Automakers account for about half of AK Steel's business, which includes manufacturing carbon, stainless, and electrical steel.<p>
Despite its wealth, AK Steel has launched a massive PR campaign stating that cuts are necessary to "remain competitive in an increasingly global steel market." AK Steel CEO Jim Wainscott sent 3,500 videotapes to the homes of union members and members of the Middletown community to try to justify his hard line.<p>
But much of his talk about "sharing the burden" falls on deaf ears. In addition to giving himself consecutive $300,000 raises over the last three years for an annual base salary of $1.68 million, Wainscott owns company stock worth $1 million and is building a $5 million house in nearby West Chester, according to union members.<p>
"This is an attack on our community," said Dan Neal, an AEIF retiree and a fourth-generation steel worker at the Middletown plant. "There is a difference between need and greed. Wainscott and the rest of management are spending tons of money busing in scabs, hiring private police to monitor our every move on the picket lines."<p>
Sonny Murphy, who has been at the Middletown plant for 29 years, assumed he was retiring in May. "They forced us out," he said. "They put us in this position. I'd like to see one of them take the benefit and retirement cut they want us to take on."<p>
AK has apparently been preparing for this lockout for some time. "They've been bringing scabs in and expecting us to train them," said Dan Corbett, a 10-year member of AEIF.<p>
In addition to providing cots for replacement workers, AK has set up kitchens to keep the factory running at full production. "They're advertising on Monster.com for crane operators with promises of $25 per hour, $500 sign-up bonuses and free room and board," striker Dan Neal said. "They bring buses right past our picket lines full of scabs, and are buying up full page ads in the 'AK urinal,'" he said--a reference to the pro-company local paper, the <i>Middletown Journal</i>.<p>
Solidarity within the community in support of the strike has been incredible. At each of the seven gates being picketed by AEIF, supporters of the strike dropped off food, firewood, hot coffee, and drinks to keep picketers' spirits up.<p>
