<p><a name="Left"></a><font face="Times New Roman, Times, serif" size="4"><b>The left and anti-Muslim racism</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THANK YOU for your editorial in response to the protests against Islamophobic cartoons. ("Muslims are right to be angry," February 10)<p>
Unlike even the liberal mainstream press, <i>SW</i> actually explained why Muslims would be so angry about a single cartoon: The historic role of cartoons in promoting oppressive policies; the fact that Islamophobia is used to sell the wars in Iraq and Afghanistan; and the racism against Muslims and Arabs in many European countries. But what is even more important is that you took a clear stand on the issue. <p>
Unequivocal opposition to a blatantly racist cartoon and firm support for those who struggle against it should be standard practice for a revolutionary socialist paper. Unfortunately, however, this was not necessarily the response in the far-left publications of Western Europe--where a clear anti-racist stand was most urgently needed.<p>
In France, the only country that has an official policy against Islamic religious practice (i.e., the wearing of the hijab in public schools), and where racism against Arabs is deeply rooted, the two main revolutionary socialist parties have not even featured a response to the cartoons on the cover page of their weeklies. <p>
<i>Lutte Ouvri&egrave;re</i> (<i>Workers' Struggle</i>) newspaper, which has long equated any assertion of Muslim rights with right-wing Islamist propaganda, even mocked French Muslims for demanding laws against Islamophobia: "But what does that mean? Should we also have laws against 'Catholicophobia' and 'Protestantophobia'? One smiles to imagine one against 'non-believer-phobia.'" (February 17)<p>
<i>Rouge</i>, the newspaper of the Ligue Communiste R&eacute;volutionaire took more care to underline its rejection of the cartoons as part of a racist, anti-Muslim campaign by the European right wing. But the article, entitled "Neither racism nor backwardness," lent just as much or more space to condemning corrupt Middle Eastern regimes, which, it claimed, re-published the cartoons in order to manipulate public opinion. (February 9) <p>
Furthermore, the article finished with a call for unity of all peoples against "those who oppose unity in the name of their religion." This leaves immigrant-bashing politicians--the real obstacle to unity--nearly off the hook. <p>
Such reactions are at best dismissive of the fight against Islamophobia and at worst play into the hands of the far right. Either way, they will hurt the left's efforts to build a strong challenge to neoliberal and nationalist parties in the 2007 presidential elections.<br>
<b>Kate O'Neil,</b> Chicago<p>

