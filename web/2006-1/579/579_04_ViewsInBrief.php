<p><a name="Fight"></a><font face="Times New Roman, Times, serif" size="4"><b>Fight for a woman's right to choose</b></font><p>

<font face="Times New Roman, Times, serif" size="3">LAST WEEK, just after the South Dakota legislature sent their sweeping anti-abortion bill to the governor for his signature, I flew into Sioux Falls for a visit to nearby Marshall, Minn. <p>
Throughout my visit, I wore a button insisting "We won't go back to the back alleys"--a button that drew a dozen women to me, each saying, "Thank you for wearing that button!" and one young woman crying, "High five!" These women--and a few men too--were very interested in talking about why abortion rights are under attack and how we can fight back. <p>
Clearly the abortion ban in South Dakota does not represent the beliefs, wishes, or the interests of many people in the upper Midwest. The more we can do throughout the country to defend abortion rights for all women, the more we can give confidence to these women and men to speak up too.<br>
<b>Nancy Welch</b>, Burlington, Vt.<p>

