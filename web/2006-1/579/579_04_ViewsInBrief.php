<font face="Times New Roman, Times, serif" size="4"><b>Stopping Nazis in Providence</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ON TUESDAY February 7, fliers from the neo-Nazi Web site/group the Vanguard News Network (VNN) were found posted all over Rhode Island College (RIC) campus. A member of the Providence branch of the International Socialist Organization was on campus and noticed one of the more than 20 fliers posted outside the Donavan Dining Center. <p>
On less than 24 hours' notice, a "Stop the Hate" rally was organized, for the next afternoon. The rally was a huge success for the usually atomized and disorganized community at RIC.<p>
Overnight, a mass e-mail was sent to all student groups, including RIC NAACP, Stonewall, and Amnesty International groups. One student spent the whole morning silk-screening "Stop the Hate" T-shirts to raise awareness of the event. Over 40 people gathered in the quad to express their outrage at the neo-Nazi infiltration of the campus. <p>
Since the VNN posters were this time exclusively anti-Semitic, many opinions were raised about the topic, connecting issues of homophobia with racism and the anti-Jewish hatred that many students feel is out of control at RIC. A majority of attendees spoke not only with outrage at the VNN, but at the complacency of the school administration and student government on such issues. <p>
The rally was a huge success, and students are now planning on continuing meetings to address this issue so that in the future the response can be stronger. With recent neo-Nazi rallies in Boston, and the horrendous hate crime that just took place in neighboring New Bedford, Mass., the RIC community takes the issue of organizing against the Nazis very seriously.<br>
<b>Jeff Schneider,</b> Providence, R.I.<p>

