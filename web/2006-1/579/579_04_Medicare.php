<font face="Times New Roman, Times, serif" size="5"><b>Left uncovered by Medicare "reform"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 10, 2006 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">I WORK in the field of mental health, and have managed or worked at group homes for people with psychiatric disabilities for several years. Most of my clients are able to live stable lives outside of mental hospitals and in the community, largely due to the development of many new anti-psychotic medications that manage the symptoms of mental illness. <p>
Now, because of new policies, many people are at risk of their lives destabilizing due to lack of medications. <p>
Across the United States, until December 31, 2005, Medicaid paid for medications for poor people with mental health issues. Co-pays were low--typically between $1 and $3 per prescription--and there were no premiums or other charges. All of that has changed. <p>
Effective January 1, 2006, the "Medicare D" prescription drug benefit--a new program touted by the Bush administration and its allies in Congress as "helpful" to the elderly and disabled--took over payment for medications for all elderly and disabled individuals enrolled in Medicare. Letters were sent out notifying Medicare recipients that they had until December 31 to select and enroll in a privately managed prescription drug benefit. <p>
In Massachusetts, default prescription drug plans were selected largely at random. Many plans cover as few as 70 out of the 100 most-prescribed medications. This is forcing doctors to prescribe alternative, covered medications that may be less effective for some of their patients. <p>
Many of the private Medicare plans charge monthly premiums. People with mental illness tend to rely on tiny Social Security benefit checks to survive (typically $500-700 per month), so even a $20 or $30 monthly cost puts a significant dent in their income. <p>
One of my clients, who I will call Rick, was enrolled in Aetna Medicare, which charges a premium of $19.04 per month, but only covers 86 out of the 100 most-prescribed medications. Rick informed me that he couldn't pay this amount. <p>
Like many people with severe psychiatric illness, he smokes heavily and isn't willing to quit. He stated that he would rather stop taking his medications and keep smoking than to fork over the monthly premium. Presently, his clinician is trying to switch Rick over to another plan.<p>
Another problem with the privatization of prescription drug coverage is that there is little regulation of the corporations that manage the Medicare benefits. At any time, these companies can choose to change which medications they cover, placing benefit recipients at risk for relapse. <p>
It is sad that it will probably only be when my clients wind up sleeping in the doorways and defecating on the front steps of Boston's well-heeled elite that such "social pillars" of the community will wake up and smell the coffee. <p>
The obvious solution to this problem is for the United States government to adopt a national health service, as other advanced industrial countries have done. It is imperative that activists continue the struggle so that adequate health care--including mental health care--is made accessible to all.<br>
<b>Bruce Burleson</b>, Boston<p>

