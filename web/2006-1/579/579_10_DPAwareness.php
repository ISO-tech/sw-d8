<font face="Times New Roman, Times, serif" size="5"><b>Hundreds attend actions against the death penalty</b></font> <p>
<font face="Arial, Helvetica, sans-serif" size="2">March 10, 2006 | Page 10</font><p>

<font face="Times New Roman, Times, serif" size="3">ACROSS THE U.S., anti-death penalty activists organized diverse events as part of Death Penalty Awareness Week. The week was called by the Campaign to End the Death Penalty and took place from February 27 to March 3.<p>
-- At Cornell University in <b>Ithaca, N.Y.</b>, the week started out big--with a rally of 300 students protesting the university's longtime reluctance to provide equal opportunities to minority students. This rally was in response to the racially motivated stabbing of a visiting Black student from Union College by a white Cornell student on February 18. <p>
Amnesty International, the National Lawyers' Guild and the ISO worked to keep energy high all week and organized five full days of events.<p>
Daily petitioning and nightly events culminated in visits from exonerated death row prisoner Shujaa Graham and the California Green Party's U.S. Senate candidate Todd Chretien. Shujaa talked about his direct experience with the racism of the prison system, and Todd laid out the need for a political alternative to Republicans and Democrats that both support the death penalty. <p>
-- In <b>Rochester, N.Y., </b>activists held a March 1 Social Justice Memorial for John Duval, who served 27 years in prison for a crime he didn't commit. John was released in 1999, became active in the "Save Tookie" campaign, helped found the Rochester chapter of the Campaign to End the Death Penalty in January, and tragically died a short time later. <p>
The memorial brought together about 50 people, some of whom had been active in winning John's freedom seven years earlier. Family and friends spoke up about John's struggles and their own.<p>
-- In <b>Madison, Wis., </b>more than 30 people came to a February 28 panel on race and incarceration at the University of Wisconsin-Madison. The event was organized by the Coalition Against Legal Lynching (CALL) and featured exonerated death row prisoner Darby Tillis, Madison Urban Ministry member Jerome Dillard and CALL member Laura Nelson. <p>
Though Wisconsin doesn't currently have the death penalty, the legislature is holding a hearing this week on possibly reinstating it. Attendees were eager both to oppose the death penalty here and show solidarity with those in other states.<p>
-- In <b>Los Angeles, </b>on February 28, activists held a screening of the movie <i>Deadline</i> followed by a discussion. The film is about the decision of former Illinois Gov. George Ryan to commute the sentences of all of the state's 167 death row prisoners in 2003. <p>
Several people who came to the screening said they were "on the fence" about the issue of the death penalty. After seeing the emotional portrayal of what the death penalty really means for inmates and family members, several people who were "on the fence" about the issue became convinced to get involved in the struggle. <p>
<i>Dana Blanchard, Brian Erway, Paul Heideman and Amanda Robertson contributed to this report</i>.<p>

