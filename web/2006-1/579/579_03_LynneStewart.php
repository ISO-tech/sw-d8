<font face="Times New Roman, Times, serif" size="5"><b>A witch-hunt to silence dissent</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 10, 2006 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">MORE THAN a year after being convicted of "aiding terrorists" and other absurd charges, veteran civil rights attorney Lynne Stewart is facing a possible prison sentence of 30 years. Last week, it was revealed that she is facing it while fighting cancer.<p>
Stewart's lawyers are requesting that her sentencing, scheduled for this week, be delayed until July so that she can complete medical treatment for her cancer. Government lawyers hadn't said if they would agree to the delay, as <i>Socialist Worker </i>went to press. But given their witch-hunt against Stewart, it's entirely possible that they will push forward and demand a harsh sentence.<p>
Stewart's "crime"? Reading a press release from her client--Sheik Omar Abdel Rahman, a Muslim cleric convicted in 1995 of conspiring with followers to bomb several New York City landmarks--to a Reuters reporter in June 2000. Two years later, using September 11 as an excuse, then-Attorney General John Ashcroft triumphantly announced Stewart's arrest.<p>
Much of the government's evidence against Stewart during the seven-month-long trial came from secret recordings of Stewart's meetings with Abdel Rahman--a clear violation of attorney-client privilege.<p>
The Feds admit that no violence ever resulted from Stewart's actions, but they continuously invoked the September 11 attacks anyway--even playing a videotape of Osama bin Laden during the trial.<p>
The witch-hunt of Lynne Stewart is designed to have a chilling effect on dissent--particularly for anyone smeared with the label of "terrorism." Still, Stewart is standing strong. "You may send me to jail for the rest of my life," she told the <i>New York Times, </i>"but at least I'll go in strong and resistant to whatever happens."<p>
Stewart's case is one of many attacks on civil liberties in the aftermath of September 11.<p>
Late last year came revelations about the scope of the administration's illegal wiretapping and spying on activists.<p>
Last week, the administration won overwhelming Senate approval for the renewal of the civil liberties-shredding USA PATRIOT ActWhile Democrats talked tough about opposing Bush's big-brother law, in the end, the Senate vote was 89-10, with 34 Democrats voting in favor. <p>
If the House approves the renewal bill as expected, then 14 of the 16 provisions of the original Patriot Act will become permanent--and two others will be renewed for four years. <p>
"Our support for the Patriot Act does not mean a blank check for the president," Senate Minority Leader Harry Reid (D-Nev.) insisted. "But the version of the Patriot Act we will soon reauthorize is a vast improvement over the law we passed hastily in 2001."<p>
Vast improvement? While the revised legislation bars the Feds from demanding that libraries turn over records of patrons' computer use, the government would still have the power to obtain this information directly from Internet service providers. Likewise, those who receive government subpoenas will have the right to challenge gag orders--but only after waiting for a full year, and remaining silent in the meantime. Provisions allowing expanded government powers to spy, search, detain and deport ordinary people were left completely intact.<p>
This is no "victory"--and it comes at the expense of permanently dismantling some of our rights.<p>

