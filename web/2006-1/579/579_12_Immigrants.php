<font face="Times New Roman, Times, serif" size="5"><b>Washington's new attack on immigrants</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Josh Gryniewicz</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 10, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">SOME 500 demonstrators from seven Midwestern states rallied February 24 in front of House Speaker Dennis Hastert's office in Batavia, Ill., to oppose legislation that includes unprecedented attacks on immigrant rights.<p>
Hastert was targeted for supporting House Resolution (HR) 4437, which brands many undocumented immigrants and permanent residents alike as "aggravated felons." Sponsored by James Sensenbrenner (R-Wis.), the proposed legislation passed the House of Representatives in December and is expected to come up in the Senate in the weeks ahead.<p>
If it becomes law, the measure would criminalize not only undocumented immigrants, but also any individual or organization assisting them, including social workers, nurses and doctors. What's more, Sensenbrenner's HR 4437 gives state and local law enforcement broader authority to act as immigration agents, while stripping federal courts of the right to review immigration matters.<p>
In short, the bill would devastate the immigrant work force, incarcerate workers and tear apart families through deportation and imprisonment while denying them access even to hearings. <p>
The only legislative opposition to Sensenbrenner has come in the form of a bill sponsored by Sens. John McCain (R-Ariz.) and Ted Kennedy (D-Mass.). Their proposal, while somewhat softer and more family-friendly, still argues that the border must be "defended."<p>
Many in the immigrant rights movement think that McCain-Kennedy is the best we can hope for, yet their bill also includes restrictions on immigrant rights. It aims to increase "border security" by creating law enforcement partnerships with Mexican authorities in order to punish immigrants on both sides of the border. <p>
The McCain-Kennedy bill also puts immigrant workers completely at the will of their employers--they would be deported if they have a 45-day gap in employment, which gives employers a fully sanctioned opportunity to exploit workers by threatening their immigration status.<p>
Furthermore, McCain-Kennedy requires employers to sponsor green cards that give immigrants legal residency--which would concentrate immigration status in the hands of the bosses and undermine organizing efforts. In addition, the bill is worded in a way that will would allow employers to fire millions of workers by citing the documentation requirement, if only to rehire them at starting salaries.<p>
For now, immigrant rights activists have been focused on stopping Sensenbrenner's HR 4437 from passing the Senate. At the protest at Hastert's office, demonstrators held signs that read "Legalize, Don't Criminalize," and brought with them 467 wooden crosses to commemorate the lives of immigrants lost crossing the U.S.-Mexican border in 2005.<p>
The protesters also confronted about 25 members of the right wing anti-immigrant group, the Minutemen Project, and shouted them down.<p>
The Minutemen's vigilante strategy in targeting immigrants isn't new. In 1977, the Ku Klux Klan launched a "border watch" to stir up racist sentiment and anti-immigrant fervor. <p>
But when the Minutemen Project attempted to organize a base in Chicago last July, they were met with a protest of 40,000, who were mobilized with the help of a Latino radio station. Since then, local activists have been confronting the Minutemen in smaller protests in the Chicago suburbs, following the example of similar grassroots struggles from San Diego to New York.<p>
Nevertheless, the Minutemen have been able to frame the national debate over immigration, with the result that both parties treat the issue of immigration as a "crisis." <p>
Against these bipartisan attacks on immigrants--fueled by the hate speech of extremists like the Minutemen Project--some 30 Chicago-based immigrant rights groups have signed on to sponsor a protest for the March 10 National Day of Action on the issue. The demands of this rally will be the legalization of undocumented immigrants, the establishment of Illinois as a sanctuary for all immigrants, and support for a general strike in defiance of the Minutemen, Sensenbrenner, and an apartheid wall on the border.<p>
In Chicago, as in cities across the U.S., immigrant rights activists see the protests against HR 4437 as part of a new chapter in the struggle for civil rights. <p>

