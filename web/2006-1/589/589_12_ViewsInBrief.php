<p><a name="Darf"></a><font face="Times New Roman, Times, serif" size="4"><b>International aid to Darfur</b></font><p>

LANCE SELFA notes that "there is not a single demand calling for the U.S. to spend money to support humanitarian aid in Darfur" ("A 'humanitarian' invasion?", May 5). <p>
In checking the Save Darfur Coalition's talking points to which Mr. Selfa referred, there is a request to "urge Congress to work with the President and the international community to make sure that sufficient food aid is provided for Darfur." Maybe food aid doesn't encourage social reform, but I think providing food to hungry people can be defined as humanitarian in this case. <p>
Though Mr. Selfa is correct in saying the "international community...has not come through," he should focus more on chastising the European Union for the relative pittance they have contributed, as well as the non-responsive Arab League, and the gluttonous, amoral trade policies of China. The U.S. has by far given the most money for relief aid. <p>
Unfortunately, that aid cannot get through because of the violence. The reason no-fly zones are needed is so the relief can be brought to the people who need it. It's hard to eat when you have helicopter gunships strafing your village. <p>
Finally, Mr. Selfa criticizes President Bush for waiting until 2004 to label the crisis a "genocide." The United Nations hasn't even done that. The president's motivations aside, by labeling the Darfur situation a genocide, the world has paid a lot more attention.<br>
<b>Bren Unger,</b> from the Internet<p>

