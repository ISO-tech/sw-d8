<p><a name="US"></a><font face="Times New Roman, Times, serif" size="4"><b>How the U.S. aided ethnic cleansing</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IN A recent letter, Marc from Cambridge, Mass., wrote that he "rejoiced" in the U.S. intervention into Bosnia, and generally in the Kosovo-Serbia situation, even though he is a pacifist ("Can military intervention save Darfur?" May 12). <p>
However, Marc seems to have ignored an important section of history--namely, after the U.S. military came in, it actually stopped the approaching movement from overthrowing Slobodan Milosovic and allowed the Serbian genocide to take place, with the complicity of U.S. generals like Wesley Clark. Let me put it another way: More genocide happened <i>after</i> we went in, than happened before. We didn't "stop a genocide" as Marc proudly claims. We helped create one. <p>
The U.S. military and the U.S. government have no interest in "helping people." Bush, Rumsfeld, Bill and Hillary Clinton, Gore, and the other crooks who run this country have an interest in protecting profits, preventing challenges to global hegemony and securing access to natural resources. <p>
Guns don't feed people and bombs don't save lives. Real economic and social aid is the solution.<br>
<b>Brian Lenzo,</b> Rochester, N.Y.<p>

