<p><a name="ABush"></a><font face="Times New Roman, Times, serif" size="4"><b>Anti-Bush or antiwar?</b></font><p>

<font face="Times New Roman, Times, serif" size="3">COMPLIMENTS TO Lee Wengraf for her story on the April 29 march in New York City ("Antiwar march takes to NYC streets," May 5). She correctly identifies the flaw in the thinking of those who made it an anti-Bush event. <p>
While I too joined lustily in the chants that berated Bush as the world's number one terrorist and railed against his henchmen (Cheney, Rumsfeld, et. al.), socialists need to stay on message that U.S. policy in Iraq is a clash of class interests. That is, U.S. policy reflects capitalist interests and imperialist aims to control oil markets. <p>
The political faces, whether Bush, Kerry, McCain, Clinton or any other representatives of the major parties, are simply fronting for those who sit in the board rooms and broker real power at the expense of the working class. <p>
Socialists need to persist in articulating the conflict of class interests that links the war in Iraq to immigrant rights to environmental exploitation to health care, and on and on. Let us struggle for power to the workers, the oppressed, the alienated and the marginalized. Organize, educate and build, that justice may prevail.<br>
<b>Richard Capron,</b> Schenectady, N.Y.<p>

