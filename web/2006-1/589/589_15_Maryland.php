<font face="Times New Roman, Times, serif" size="5"><b>Marching against the death penalty in Maryland</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alex Bennett </b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 19, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">BALTIMORE--"They say death, we say no! Take Vernon Evans, off death row!" chanted approximately 50 abolitionists during a May 6 rally outside the Supermax prison here at the kickoff of a 25-mile march.<p>
Two days later, the marchers arrived in Annapolis, Md., as Evans' lawyers presented new arguments against his death sentence before the state's highest court. <p>
The court heard appeals based on the pattern of racism found in a state-commissioned University of Maryland study of the state's capital punishment system, evidence that mitigating circumstances in Vernon's childhood were not presented in his original trial, and flaws in Maryland's lethal injection protocol. <p>
The court's decision could become a significant turning point in the struggle to save Vernon Evans--and everyone else on Maryland's death row. <p>
Activists sought to draw public attention to the hearings through the weekend-long march. "We're marching to let citizens throughout the state know that this is an issue that we have the conviction to speak out about," explained Donnie Evans, Vernon's cousin and member of the Baltimore Coalition Against the Death Penalty. <p>
Dubbed "From the Death House to the Courthouse," the action was organized by the Baltimore Coalition, the Campaign to End the Death Penalty and Maryland Citizens Against State Executions.<p>
On the first night of the march, activists held a community forum on the death penalty at St. Alban's Episcopal Church in the Glen Burnie suburb. Speakers included exonerated Illinois death row inmate Darby Tillis, who described his tribulations inside the criminal justice system. Vernon himself also called in "Live from Death Row," but after a few minutes, his call was terminated by prison authorities.<p>
Vernon was sentenced to death for two contract killings in Baltimore County, a predominantly white, suburban area outside of the city with a proven penchant for executing African Americans. Prosecutors used eight of their 10 challenges against Black jurors, winning the case using testimony from a jailhouse snitch, despite the lack of any physical evidence linking Vernon to the murders.<p>
The court will probably not hand down its decision until fall, providing more time for abolitionists to build the fight to save Vernon Evans and to abolish Maryland's death penalty.<p>
"From the Death House to the Courthouse" was unquestionably a huge step forward for the abolition movement in Maryland, forging bonds between multiple groups in the region, and turning Vernon's trial into a three-day media event that made Maryland's racist and anti-poor death penalty impossible to ignore.<p>
<i>David Gregal contributed to this report</i>. <p>

