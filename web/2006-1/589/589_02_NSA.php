<font face="Times New Roman, Times, serif" size="4">NSA exposed for demanding telephone records of U.S. citizens</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Big Brother knows who you're calling</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 19, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">HAS BIG Brother been tracking your phone calls? Probably, according to a report last week in <i>USA Today</i>.<p>
Three of the nation's four largest telecommunications companies--AT&amp;T, Bell South and Verizon--were reportedly paid by the government to turn over tens of millions of customer phone records to the National Security Agency (NSA) since the September 11 attacks. The Bush administration claims that this invasion of privacy is necessary to catch "terrorists," by examining the patterns of various phone users' records.<p>
The latest revelations about the Bush administration's shredding of civil liberties come after a <i>New York Times</i> report in December that the administration authorized the NSA to conduct eavesdropping on international calls and spying on international e-mails without warrants on suspected "terrorists."<p>
At that time, administration officials insisted that the NSA was focused exclusively on international calls--and that law-abiding Americans had nothing to fear. In front of the Senate Judiciary Committee in February, for example, Attorney General Alberto Gonzales promised, "Only international communications are authorized for interception under this program...To protect the privacy of Americans still further, the NSA employs safeguards to minimize the unnecessary collection and dissemination of information about U.S. persons."<p>
When questioned why the government would limit itself to international calls and e-mail, Gonzales added, "I believe it's because of trying to balance concerns that might arise that, in fact, the NSA was engaged in electronic surveillance with respect to domestic calls."<p>
Last week's revelations cast doubt on the assurances from Gonzales and other administration officials. As one unnamed official told <i>USA Today</i>, the program includes the compilation of a massive database that aims to be a record of "every call ever made" <i>inside </i>the U.S.<p>
The records turned over to the government include numbers called, date, time and other details--though not an actual record of what was said in the conversation. Customer names and addresses, while not included in the call records, are fairly easy for the government to cross-reference. And sources told <i>USA Today</i> the government admitted that, in addition to the NSA, other agencies like the FBI, CIA and Drug Enforcement Agency might also have access to whatever information was handed over. <p>
The program is a blatant violation of the law, according to some experts--no matter how the Bush administration tries to spin it. "If they don't get a court order, it's a crime," Kate Martin, director of the Center for National Security Studies, told the <i>New York Times</i>. According to Martin, federal law stipulates that the NSA is banned from carrying out this kind of surveillance without court approval.<p>
Historically, AT&amp;T and other phone companies required a court order before they would turn over any part of a customer's calling data. But after September 11, the government made a pitch to the companies that the information was vital to "national security"--and apparently offered to pay the phone companies for the information.<p>
Of the major telecommunications companies at the time, only Qwest declined to cooperate with the government's fishing expedition. The NSA reportedly threatened Qwest for its refusal, saying that the company might face a tougher time getting government contracts in the future<p>
When Qwest's lawyers asked the NSA to get approval for its plan through the Foreign Intelligence Surveillance Act (FISA) court--a court that almost never says no to government requests for spying--NSA officials refused. "They told [Qwest] they didn't want to do that because FISA might not agree with them," one person told <i>USA Today</i>.<p>
So the Bush administration apparently fell back on "plan B"--doing whatever it wanted anyway, but justifying it under the guise of "national security."<p>

