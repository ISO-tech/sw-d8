<font face="Times New Roman, Times, serif" size="5"><b>Forty years of the Black Panthers</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Michelle Simon</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 19, 2006 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Black Panther Rank and File,</b></i> showing at the Yerba Buena Center for the Arts in San Francisco until July 2.</font> <p>

<font face="Times New Roman, Times, serif" size="3">THE CIVIL rights movement of the 1950s and '60s inspired a generation of young people to become politically active in a way never seen before.<p>
October 2006 will mark the 40th anniversary of the Black Panther Party for Self Defense. Founded by Huey Newton and Bobby Seale when they were students at Laney College in Oakland, Calif., the Black Panther Party was influenced by Marxism and mixed radical Black nationalism with Third World and community service politics.<p>
In October, Panthers from all over the country are expected to descend on Oakland. In celebration of the Panthers' activism, San Francisco's Yerba Buena Center for the Arts is presenting the stunning exhibit <i>Black Panther Rank and File.<p>
</i>The exhibit displays works by artists inspired by the Black Panther Party as well as rare artifacts and never-seen-before documents, film, recordings and photographs. It kicks off with photographs by Ruth-Marion Baruch and Pirkle Jones, whose photos of a rally to free Huey Newton show people in the Black Panther Party as real people with human faces and emotions in contrast to the negative mainstream media depictions as angry, violent and militant Blacks.<p>
There are also photographs by Ilka Hartmann, a young German student at Berkeley who was inspired by the struggle and photographed all the party's public events. Most of her photos were pictures of a 1968 memorial rally for Bobby Hutton--the party's 17-year-old treasurer who was murdered by Oakland police when they raided party headquarters.<p>
The exhibit includes an impressive display of the Black Panther Party newspapers as well as articles from the paper.<p>
Most people are familiar with the free breakfast program in which they served free meals to school-aged Black children, but the Panthers also organized food co-ops. One article includes instructions for how to start a food cooperative in your community.<p>
The exhibit also includes two videos of Newton being interviewed in jail at the Alameda County courthouse in Oakland.<p>
There were two pieces that were the most striking for me. The first was a mural on the wall of the gallery depicting familiar Oakland streets as important roles in the history of the Black Panther Party. The other was an aural map of Oakland marking areas of historical significance, such as the Black Panther Party headquarters, the Alameda County courthouse and the north Oakland neighborhood where many members lived. <p>
This exhibit makes me appreciate the historical significance of my city as the birthplace of the Panthers.<p>

