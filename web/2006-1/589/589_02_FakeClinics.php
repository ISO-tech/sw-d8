<font face="Times New Roman, Times, serif" size="4">Anti-abortionists target teenager in Indiana</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Harassed at fake clinics</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 19, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">WHEN A 17-year-old Indiana woman walked into what she thought was a Planned Parenthood clinic to schedule an abortion, she was in for a frightening surprise.<p>
According to a report from Planned Parenthood, the girl, accompanied by her mother and boyfriend, unknowingly walked into a so-called "crisis pregnancy center" that shares a parking lot with a real Planned Parenthood clinic.<p>
Despite the benign-sounding name, "crisis pregnancy centers" are a front for anti-abortion organizations bent on coercing women out of terminating their pregnancies. Women who walk into these centers are subjected to harassment and manipulation. The right-wingers use doctored pictures of supposedly aborted fetuses and patently fake "scientific" information--claiming, for example, that abortion causes breast cancer or fetuses feel pain during abortions.<p>
In the case of the Indiana teen, the staff at the fake center took down her personal information, telling her to come back to their "other office" (the real Planned Parenthood clinic) for a future appointment. When she returned for her appointment, the Planned Parenthood staff had no record of her, but the police were waiting--because the fake center had called, claiming that a minor was being forced to have an abortion against her will.<p>
The anti-choice staff at the fake clinic didn't stop there, either. They proceeded to turn the young woman's life into a nightmare--showing up at her home, calling her father's workplace and even going to her school to urge her classmates to pressure her not to have an abortion.<p>
Nationwide, these fake centers actually outnumber abortion providers by six to one. And no wonder, considering that the Bush administration has given away more than $60 million to such programs for promoting abstinence-only sex education.<p>
In late March, Rep. Carolyn Maloney (D-N.Y.) introduced the Stop Deceptive Advertising for Women's Services (SDAWS) Act, which would require the Federal Trade Commission (FTC) to enforce truth-in-advertising standards for these fake clinics.<p>
As Joan Malin, president of Planned Parenthood of New York City, told Planned Parenthood's <i>Choice!</i> Magazine, "Deception and lies don't help women make informed choices. Too often women are given incomplete or even inaccurate information that leaves them misinformed, confused, and afraid."<p>

