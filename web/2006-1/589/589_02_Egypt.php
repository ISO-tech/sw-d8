<font face="Times New Roman, Times, serif" size="5"><b>Pressure builds against Egypt's new crackdown</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Mostafa Omar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 19, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE EGYPTIAN government of President Hosni Mubarak continueed to crack down on a growing pro-democracy movement as activists in the U.S. mobilized pickets outside Egyptian consulate buildings in support of those arrested.<p>
On May 11, 10,000 riot police accompanied by hundreds of plainclothes thugs cordoned off downtown Cairo. The state aimed to stop a demonstration to show support for two judges facing disciplinary hearings for publicly accusing the Mubarak regime of rigging last November's elections. <p>
As hundreds of marchers peacefully tried to make their way to the High Court, the police and thugs beat dozens of people, including reporters with Reuters and the Al Jazeera news channel. <p>
Since the police attack, the government has arrested more than 200 people, bringing the total number of pro-democracy activists jailed to 260. The activists--including members of the Egyptian Movement for Change (Kefaya or Enough!), the Muslim Brotherhood and the Center for Socialist Studies, among others--were detained under an emergency anti-terrorism law that the government promised it would not use against citizens when it was renewed recently. <p>
However, the crackdown seems to have solidified the opposition's will to fight back. In the Tora Jail in Cairo, jailed activists launched a hunger strike May 13 and won better conditions and access to newspapers. <p>
One hundred and fifty college professors sent a letter to the government demanding the immediate release of all students to allow them to attend final exams. Other judges remain overwhelmingly supportive of the two facing hearings.<p>
Plus, Kefaya and other groups are planning a national day of sit-ins on May 25 to commemorate the regime's brutal assault on pro-democracy women protesters a year ago and to demand an end to the emergency law. <p>
In the U.S., the Center for Economic Research and Social Change organized pickets in four cities May 9 in support of the jailed Egyptian activists. The protesters attempted to deliver a protest statement to consulate officials.<p>
"We wish to express our distress at the brutal treatment of peaceful demonstrators calling for legal reform," reads the statement. "We oppose this attempt to silence voices in the judiciary and the media who have exposed electoral fraud, state violence and corruption...We call upon the Egyptian authorities to show respect for human rights and the independence of the Egyptian judiciary by releasing the detainees without delay.<p>
The statement has been signed by well-known antiwar and social justice activists such as Howard Zinn, Noam Chomsky, John Pilger, Alexander Cockburn, Boris Kagarlitsky, Jeffrey St. Clair, Deepa Fernandes, Anthony Arnove and Dave Zirin.<p>
