<font face="Times New Roman, Times, serif" size="5"><b>Wisconsin voters send an antiwar message</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Aileen Paguio</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 14, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">VOTERS ACROSS the state of Wisconsin have sent a message to Washington: Bring U.S. troops home from Iraq--right now.<p>
Thirty-two cities and towns--ranging from the state capital of Madison to the tiny northern village of Exeland--had referendums on the April 4 ballot calling for the immediate withdrawal of troops from Iraq. The measure passed in three-quarters of the cases. The statewide antiwar margin from all 32 elections where the referendum was voted on was 61 percent to 39 percent.<p>
The campaign around the referendums began last year when activists gathered signatures to get on the ballot. In Madison, more than 300 volunteers gathered 50 percent more signatures than required, turning in some 19,000.<p>
"This is truly a grassroots effort," said Keith Schmitz, who lives in the Milwaukee suburb of Shorewood, where he went door to door with literature about the town's referendum. "None of us are James Carvilles; it's just do-it-yourself politics. We're just doing our best and seeing what happens."<p>
On the evening of the vote, Madison volunteers packed Hawks, a local bar and restaurant, to await the results. Announcements of victories in town after town were greeted with chants of "This is what democracy looks like!" and "The people united will never be defeated!" <p>
The results are an embarrassment for pro-war politicians who claim--in spite of collapsing approval ratings for George Bush and his handling of the war--that opposition hasn't reached the "heartland." Conservative pundits tried to dismiss the results as reflecting anti-Bush sentiment in a handful of liberal towns.<p>
The out-now referendum did pass easily in Madison--home of the main state university campus and known for being progressive. But it succeeded more widely.<p>
As liberal columnist John Nichols pointed out, in the 2004 presidential election, voters in the town of Luxemburg picked George W. Bush by an almost 2-to-1 margin. Last week, though, they passed the antiwar referendum with a clear majority. Likewise, Casco, Ephraim, Ojibwa, Draper and Edgewater all backed Bush two years ago--and all voted against the war last week.<p>
In the western city of La Crosse, Republican opponents of the referendum spent almost $700,000 on 'no' vote ads, but the measure passed by a comfortable 55 percent to 45 percent margin. The town of Evansville had a competing referendum urging support for George Bush--it was rejected.<p>
Other states and cities have seen similar efforts. Last year, 49 towns and cities across Vermont passed antiwar resolutions at town meetings in what the <i>Christian Science Monitor </i>called "a town-meeting revolt over the Iraq war." <p>
Now, the Wisconsin vote could add new momentum to such initiatives. Steve Burns, coordinator of the Wisconsin Troops Home coalition, says that he has received a dozen calls from California, New York, Illinois and Minnesota about how to initiate a referendum. Milwaukee already has a similar out-now referendum on the ballot in November.<p>

