<font face="Times New Roman, Times, serif" size="5"><b>How LA hospitals get rid of patients</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 14, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">HOSPITALS IN southern California are giving homeless and poor patients a free ride--straight to Los Angeles' Skid Row. Three hospitals have acknowledged that poor patients discharged from their facilities were put in taxicabs to be dropped off in the city's downtown Skid Row.<p>
The issue made the headlines again last month when a security camera outside the Union Rescue Mission recorded a 63-year-old woman named Carol Reyes wandering the street, wearing only a hospital gown and slippers.<p>
After a few minutes, mission worker Regina Chambers approached her and offered assistance. "She was very disoriented, didn't know where she was or what she was doing," said Chambers. "All she knew is that she had been to a hospital."<p>
It was later discovered that the Kaiser Permanente hospital in the city of Bellflower, 16 miles southeast of the mission, discharged Reyes and paid for her cab ride to Skid Row.<p>
The issue was first in the news in December when Los Angeles Police Department Capt. Andrew Smith threatened criminal charges against hospitals that dump poor patients. Smith said that several suburban police departments also drive into the city to dump poor and homeless people on Skid Row.<p>
Some patients have been found with two or three hospital bracelets on, clearly unable to fend for themselves. One was even found wearing a colostomy bag.<p>

