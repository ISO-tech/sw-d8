<font face="Times New Roman, Times, serif" size="5"><b>San Diego speaks out: "&iexcl;S&iacute; se puede!"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 14, 2006 | Page 5</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>MARJORIE BERRY reports on San Diego's huge immigrant rights demonstration.</b><p>

<font face="Times New Roman, Times, serif" size="3">SAN DIEGO is known for its perfect weather, affluent coastal communities and military bases. All that changed on April 9 as more than 100,000 workers and students took to the streets to show support for immigrants and demand amnesty.<p>
The streets of San Diego were flooded with a multicultural march that made its way from a rally at San Diego City College, up to Balboa Park, and then to another rally at the San Diego Administrative Offices. <p>
Chants of "&iexcl;Escuela s&iacute;, guerra no! &iexcl;Amnist&iacute;a s&iacute;, migra no!" could be heard all the way across town, louder than the police sirens and the media helicopters. As the march made its way through the streets, people of all ethnicities could be seen leaving their cars and houses to join in the growing movement.<p>
Not only was the size of the demonstration an awe-inspiring sight in such a conservative town, but the political impulses of the participants were also exceptional. <p>
"I'm not here just because I'm a Mexican," said a young San Diego City college student, who summed up the sentiment of the crowd. "I'm here because there should be justice for everybody. This is about equality for all. It's not just about Mexicans stepping out into the streets--it's about workers stepping into politics."<p>
The crowd was full of people, communities and organizations that had come to the same conclusion--an attack on one worker is an attack on all. African Americans, Native Americans, Asian Americans and European Americans all came together to oppose the racist Sensenbrenner bill known as HR 4437. <p>
A large number of marchers were young people, who, despite what the conservative media would have people think, knew exactly why they were walking out of classes a week earlier. "Of course I know why we're here," one sixth-grader from St. Jude Academy said. "There are people being discriminated against and made into criminals. They're my mom, my dad, my cousins. They're me, they're my future!" Her classmate added, "At school, the rich teach us about their democracy. Here, we show them ours." <p>
In the conservative, military town of San Diego, activists are often asked whether working people could ever really put aside their differences and make real change. For anybody still wondering this, they need only open their windows and listen to the voices still ringing through the streets of San Diego: "&iexcl;S&iacute;, se puede!"<p>

