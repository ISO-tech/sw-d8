<font face="Times New Roman, Times, serif" size="4">Tragedy in California:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>We won't forget Anthony Soltero</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 14, 2006 | Page 1</font><p>

<font face="Times New Roman, Times, serif" size="3">SCHOOL OFFICIALS threatened to punish 14-year-old Anthony Soltero for standing up for immigrant rights. Now he's dead as a result of their threats.<p>
Anthony's tragic suicide has shaken and saddened people around the country--but also made them more determined to continue the struggle for equality that he was a part of.<p>
"He was just fighting for his rights," Anthony's mother, Louise Corales, said at a Palm Sunday mass for Anthony at Our Lady of Guadalupe Church in Ontario. "He would be proud that we're here now to honor him because he is a hero."<p>
Anthony, an eighth-grader at DeAnza Middle School in Ontario, Calif., west of Los Angeles, helped organize a student walkout in the week following the 1 million-strong March 25 demonstration in LA against anti-immigrant legislation that would brand undocumented workers as felons.<p>
Tens of thousands of students participated in the walkouts across Southern California--facing harassment and abuse from both police and school officials. In Anthony's case, the threats had tragic results.<p>
According to civil rights lawyer Sonia Mercado, Anthony was called into a school administrator's office on March 30 for questioning about the walkout. The administrator told Anthony that he could be barred from attending graduation, that his mother could be fined $250 for his truancy, and that he could go to jail for three years, Mercado told the <i>Press-Enterprise, </i>a local newspaper.<p>
Anthony was a top student, but had been disciplined for having a pocketknife at school and was nearly finished with a term of community service and probation, Mercado said.<p>
Anthony called his mother from home to tell her about the threats. But before she could arrive back, he had shot himself in the head. He left behind notes apologizing to his mother--and asking her to keep his younger brothers from seeing his body.<p>
"Anthony was born here, and his mother was born here," said Nativo Lopez, president of the Mexican American Political Association and an organizer of the March 25 demonstration in LA, who met with Anthony's family last week. "This is an example of a native-born citizen who identifies with this movement and takes a leadership role in a middle school to organize a walkout of his peers. He did this, and the school authorities threatened him with incarceration if he did it again."<p>
In a bitter twist, Ontario police say they will ask prosecutors to charge Anthony's stepfather with a felony--for not keeping his gun locked up.<p>
But the people who ought to bear responsibility in this tragedy--officials of the local school district--this week defended to reporters their policy of punishing students who participated in the walkout. During the walkout itself, the school district asked police to round up protesting students and issue them citations--something even the cops who now want to send Anthony's stepfather to jail refused to do.<p>
Lopez says that this attempt to punish students for exercising their rights--which certainly isn't confined to Ontario--is "absolutely repugnant."<p>
"It's fine if they read passages from the Constitution, the history of the country, biographies of George Washington and Thomas Jefferson--who the English, back in the era of the American revolution, accused of terrorism," Lopez said. "But then these children who are willing to practice these same precepts and theories can be reprimanded for actually living the Constitution."<p>
Students in Los Angeles have planned a march for immigrant rights on April 15, which they have named in Anthony's honor. They have called on students around the country to hold events on the 15th to honor Anthony Soltero and his commitment to justice.<p>

