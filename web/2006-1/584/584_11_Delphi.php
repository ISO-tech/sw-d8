<font face="Times New Roman, Times, serif" size="4">Delphi asks bankruptcy court to void union contracts</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Will the UAW fight at Delphi?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 14, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">WILL THE UAW stand up to Delphi's efforts to shred their union contract in bankruptcy court?<p>
One week after Delphi CEO Steve Miller asked a federal bankruptcy judge to cancel union contracts--a ruling is several weeks away--the union hasn't even called for a strike-authorization vote. <p>
Meanwhile, Miller announced that Delphi would retain just eight of the company's 21 auto parts plants and proceeded with an earlier plan to slash wages. The rest of the factories--which were once part of General Motors--would be sold off or closed altogether.<p>
The announcement came in the wake of a UAW-negotiated offer by GM and Delphi that would offer a $35,000 buyout for early retirement to workers at the company with at least 27 years on the job, plus an opportunity for 5,000 Delphi workers to "flow back" to GM.<p>
Under Miller's plan, those who stay on the job would see their wages progressively cut. Miller's message is clear enough: whether or not the judge okays the cancellation of union agreements, he will continue to slash and burn--so workers should take whatever money they can get now.<p>
Although UAW leaders have said that any attempt to cancel union contracts would provoke a strike, UAW President Ron Gettelfinger hasn't initiated the strike authorization vote process mandated by the union's constitution.<p>
"A week after the bankruptcy court announcement, the only thing we have heard from the union is about the buyout, which is about limiting GM's liability and undermining solidarity," said Gregg Shotwell, a Delphi worker at the Coopersville, Mich., plant. "Without more information, people don't know what all the issues are, or what they are going to do."<p>
Shotwell has emerged as a prominent member of the group Soldiers of Solidarity (SOS), a network of UAW dissidents who began organizing after Delphi declared bankruptcy last fall and the UAW pushed through wage cuts and a rollback of retiree health care benefits. Based on longtime UAW oppositionists and those new to union activism, SOS has pushed for a work-to-rule campaign at Delphi and held meetings of rank-and-file workers in town-hall style forums in cities across the U.S.<p>
With Gettelfinger and the UAW hierarchy working behind closed doors, SOS has given a voice to the anger and bitterness among autoworkers. "We have to give a unified response," to Delphi's attacks, Shotwell said. "If the union is going to allow Delphi to isolate us, we will be destroyed, and [the employers] will move onto the next big bargaining unit" to attack--starting with the 2007 master auto contract talks.<p>
For Delphi and GM workers who are too young to retire, taking the buyout means getting six or seven months' wages, and then relying on a pension that may not be there in the future, if the companies follow the example of several airlines and dump retirement benefits altogether.<p>
Even if that doesn't happen, new hires--who will make $14 an hour under the 2003 UAW-Delphi contract--could be encouraged by union leaders to approve cuts in retiree benefits in future contracts. <p>
Workers who accept the buyouts may think they are leaving behind those ineligible for the deal, Shotwell said. "But," he added, "our future is in their hands, too."<p>

