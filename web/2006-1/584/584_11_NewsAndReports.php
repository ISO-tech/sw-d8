<p><a name="Clinton"></a><font face="Times New Roman, Times, serif" size="4"><b>Protesting Hillary Clinton</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jane Hamlin and Shaun Joseph</b></font><p>

<font face="Times New Roman, Times, serif" size="3">PROVIDENCE, R.I.--Antiwar activists protested and disrupted a speech by Sen. Hillary Clinton (D-N.Y.) at Brown University on April 8. <p>
After receiving no response to their demand that the prowar Democrat meet with Rhode Island military families and Dr. Sureya Sayadi, an Iraqi Kurdish anti-occupation activist touring with the Code Pink Iraqi Women's Delegation, 75 people turned out to rally against Clinton.<p>
Dr. Sayadi spoke before the rally at an antiwar forum at Brown about the horrific results of the U.S. war, supported by leaders of both parties. She also spoke of Hillary Clinton's trip to Iraq, where Clinton praised the building of permanent bases in the country to use as a launching pad to bomb Iran.<p>
Clinton's lecture on "Women and Leadership" was interrupted by antiwar hecklers loudly demanding, "Is it leadership to continue to support Bush's war?" and "What about the women of Falluja?" The speech was interrupted for 10 minutes before police and Secret Service agents physically removed the protesters.<p>
The forum and protest was sponsored by the American Friends Service Committee, Brown Faculty and Students Against the Occupation of Iraq, the Code Pink Iraqi Women's Delegation, the International Socialist Organization, Military Families Speak Out, and the R.I. Community Coalition for Peace.<p>
