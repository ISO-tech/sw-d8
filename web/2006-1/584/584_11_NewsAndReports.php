<p><a name="War"></a><font face="Times New Roman, Times, serif" size="4"><b>We say no to war</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Shane Johnson</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CINCINNATI--Approximately 50 antiwar activists from the Intercommunity Justice and Peace Center, Iraq Veterans Against the War and the newly formed chapter of the Campus Antiwar Network gathered outside the Great American Ballpark in frigid, windy conditions to protest President Bush's throwing out of the first pitch at the Cincinnati Reds baseball game on opening day. <p>
Bush's presence in the city forced the opening day organizers to shut down much of the downtown area--including passage on the river--and to institute a "no-fly" zone over the city. Protest organizers handed out nearly 3,800 leaflets explaining the "Three strikes against the Bush regime," and chanted "Peanuts cost a dollar, this war costs $195 million per day" and "Double play: End the war in Iraq and bring the troops home now!"<p>
