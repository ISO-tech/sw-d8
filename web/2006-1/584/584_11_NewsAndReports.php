<p><a name="Fight"></a><font face="Times New Roman, Times, serif" size="4"><b>Fight for immigrant rights</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Rayyan Ghuma</b></font><p>

<font face="Times New Roman, Times, serif" size="3">COLLEGE PARK, Md.--On April 6, 65 students at the University of Maryland (UMD) attended a teach-in entitled "UMD Stands Up for Immigrant Rights: No to HR 4437; Amnesty Now!" <p>
The Latino Student Union, the International Socialist Organization, the Asian American Student Association, and Community Roots put together a panel, fielded questions, and initiated discussion in response to the recent attacks on undocumented workers. Attendees learned that one out of 14 Asian immigrants are undocumented, and that 75 percent of citizens are against the building of a border wall between the U.S. and Mexico.<p>
After listening to ISO member Derron Thweatt unapologetically advocate amnesty, students discussed the provisions of HR 4437, the history of civil strife in Central and South America and its connection to U.S. imperialism, and the importance of standing against the racist intentions of all proposed "compromise" bills.<p>
The discussion finally ended with a call for UMD students to mobilize on April 10 in Washington D.C. With more than 11 million undocumented workers directly affected by the on-going war on immigrants, we must unite against this unjustified witch-hunt against innocent people.<p>

