<font face="Times New Roman, Times, serif" size="4"><b>Stop the Minutemen</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Mike Carlson</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--More than 200 people gathered downtown on April 1 to protest the local branch of the anti-immigrant vigilante group, the Minutemen. <p>
The Minutemen had announced on their Web site that they were planning to go to possible different locations in Seattle to harass immigrant day laborers. This included a location downtown, where counter-protesters from many local activist groups first met up--a frequent meeting place for immigrants looking for work in Seattle.<p>
After it was clear that the Minutemen were not going to show up, protestors found out that the Minutemen had planned to go to several Home Depot stores around the Seattle area to confront immigrant workers. In defense, groups of activists organized to travel out to each of these locations, ready to confront the Minutemen.<p>
Fortunately, the Minutemen decided not to show up to any of these locations. Instead, activists turned the counter-protests against the Minutemen into a protest for immigrant rights, holding pro-immigrant signs in the parking lots and on the streets near each location. Many were met with honks from passing cars in support of fellow community members.<p>
