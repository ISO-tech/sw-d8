<font face="Times New Roman, Times, serif" size="4">McKinney scapegoated after confrontation with police</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Blaming the victim on Capitol Hill</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 14, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">REP. CYNTHIA McKinney (D-Ga.) became the focal point of Republican rage last week for daring to call her manhandling by Capitol Police by its name--racial profiling.<p>
"She's a racist," said Rep. Tom DeLay, the Republican representative from Texas--who a day earlier announced that he would resign from the House amid a corruption scandal. "If somebody else doesn't file an ethics charge, I will," said DeLay on Rush Limbaugh's radio show.<p>
This from a man who was arrested October 20 on conspiracy and money-laundering charges.<p>
But McKinney's Democratic Party colleagues did nothing while DeLay and right-wing media pundits lashed out at the most unrepentant antiwar member of Congress.<p>
The altercation began when a police officer failed to recognize the six-term congresswoman McKinney as she stepped around a security checkpoint to enter a Capitol Hill office building. <p>
Members of Congress aren't required to pass through security, and they're given a special lapel pin to identify them. McKinney wasn't wearing the pin, which frequently goes unworn because police usually recognize elected representatives. <p>
Apparently McKinney, a Black woman, didn't fit the officer's mental picture of who belongs in the corridors of power. He grabbed her arm, and she allegedly responded by jabbing him with her cell phone.<p>
Given the denunciations of McKinney, you'd never know that Capitol police went to her office and apologized for the officer's conduct. <p>
McKinney issued a statement of regret the day after the confrontation. But the episode continued to escalate because her fellow Democrats allowed it to.<p>
McKinney has repeatedly bucked the party line with her opposition to Bush's war on Iraq and was one of only three Democrats to call the Republicans' bluff and vote for a resolution for the immediate U.S. withdrawal from Iraq.<p>
In 2002, the Democratic Party establishment supported her opponent in the primary, which led to her losing her seat. When she won it back two years later, House Minority Leader Nancy Pelosi refused McKinney the usual courtesy of retaining her seniority, turning her into a freshman legislator despite her five previous terms in office.<p>
Pelosi and company used this latest controversy to force McKinney to back down entirely. Under pressure from her own party, she voted along with her colleagues for a resolution praising the Capitol Hill police for their professionalism.<p>
By forcing McKinney to cave, the Democrats undercut the seriousness of racial profiling experienced by millions of African Americans at the hands of police across the U.S.<p>

