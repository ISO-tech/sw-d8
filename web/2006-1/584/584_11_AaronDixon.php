<font face="Times New Roman, Times, serif" size="5"><b>Seattle activist challenges politics as usual</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicholas Hart</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 14, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--Nearly 300 people gathered at the Garfield Community Center on April 8 for food, entertainment and energizing speakers at the kickoff rally for Aaron Dixon's Green Party-backed campaign for U.S. Senate in Washington State. <p>
Dixon is the founder of the Seattle Black Panther Party and a widely-respected community leader who has been fighting for peace and justice for 40 years. His campaign is already gaining broad support among Seattle's activist community, as evidenced by the growing list of his endorsers and the large multiracial turnout.<p>
The theme throughout the evening was voiced by Rev. Jeffrey from the New Hope Baptist Church: "I'm tired of conditions in the community. I'm tired of 37 million people living in poverty." The reverend linked the devastation of the Gulf Coast to the systematic destruction of the poor and their communities and exclaimed, "I'm tired of our people being the first in line for disasters--economic or social."<p>
Elaine Brown, former chairperson of the Black Panther Party, spoke at length about the war abroad and poverty and racism at home. She criticized Democrats like incumbent Sen. Maria Cantwell for supporting the U.S. war in Iraq and for their attacks on the poor. "We have to get a movement back together," Brown urged the audience, "and open a door for people with no hope."<p>
"The people are tired and they want change," said Aaron Dixon, echoing the earlier speakers. Dixon pointed out that we live in the wealthiest country in the world and said that everyone should have a decent education, job and health care. <p>
He described his travels in South America and the tremendous victories being won across the continent by organized grassroots movements. "They say it can't be done, but in Bolivia they shut their country down, kicked out two presidents and elected the first indigenous president in Latin America," he commented.<p>
Dixon pointed out the exciting potential of the recent immigrant-rights protests sweeping the U.S. "We've watched the Latino community come alive," he said, adding that "we the people have had enough! We're going to take our country back!"<p>
Other speakers included Amy Hagopian, president of the Garfield PTSA, which passed a resolution opposing military recruiters on campus last year; Juan Jose Bocanegra, a long-time community organizer; and Priest Amon, the president of the Seattle Central Community College Black Student Union.<p>
The rally recruited dozens of new volunteers for the campaign, and showed the potential to build a united fight for justice. As Tajuan LaBee, a freelance writer who attended the rally, said, it's exciting "to see a candidate that actually talks about things that matter." <p>

