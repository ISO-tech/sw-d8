<font face="Times New Roman, Times, serif" size="4">Government scraps hated labor law</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Victory for the French protests</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 14, 2006 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>JESSIE KINDIG reports from Paris.</b><p>

MASS MOBILIZATIONS of students and workers--the largest in France since the famous rebellion of May 1968--forced the right-wing government to withdraw a hated new labor law.<p>
At the beginning of the week, President Jacques Chirac announced that the First Employment Contract (known by its French initials CPE) would be scrapped. It was a humiliating surrender for the French government, led by Chirac's fellow conservative, Prime Minister Dominique de Villepin, who as late as last week had insisted that the law would go into effect as planned.<p>
The CPE would have created a 2-year "trial period" for workers under 26, during which they could be fired without cause or notice.<p>
When the measure was first proposed earlier this year, students at universities and high schools began mobilizing escalating protests. For most of March, two-thirds of the country's universities were on strike or occupied, along with growing numbers of high schools.<p>
France's main unions threw their weight behind the struggle, organizing mass demonstrations, and then calling two days of national strikes that mobilized millions of people for demonstrations across the country.<p>
The second strike took place last week, on April 4, with almost 3 million people taking to the streets for demonstrations for the second week in a row. Schools, public transportation and a range of other industries and services were paralyzed. In Lannion, an industrial town that is a hub for telecommunications factories in France, 7,000 people joined the protests--more than one in three of the town's population.<p>
The protests shook French society like no event since 1968--prompting former French President Val&eacute;ry Giscard d'Estaing to warn last weekend that the "normal functioning of institutions must be re-established" by scrapping the law. This was the latest of many such statements from the French political establishment recognizing, in contrast to de Villepin's continuing bluster, that the law would have to be withdrawn.<p>
"It's the first retreat by this government," the Ligue Communiste R&eacute;volutionaire declared in a statement. "It bodes well. It's the first time since the election of Chirac in 2002 that power has conceded under the pressure from a mobilization of youth and workers. Struggle pays."<p>
While the CPE is dead, the struggle isn't over. This week, the government will announce plans for replacing the labor law.<p>
The ruling conservative party in parliament, the UMP, led by Interior Minister Nicholas Sarkozy, is promising measures to help impoverished youth to find employment, rather than a repeat of the CPE. But no one should trust Sarkozy. This is the man who called North African and Arab youth "scum" after they rioted last year in the Paris suburbs over police violence.<p>
The Intersyndicale--a group of union leaders and spokespeople of the official high school and university student unions, which negotiated last week with representatives of the UMP--accepted Chirac's statement at the beginning of the week, and called for the blockades and occupations of universities to be lifted.<p>
But more radical sections of the movement want to see what the government's next move is.<p>
The National Student Coordinating Committee for the university strike--which meets in a different city each week, and whose delegates are re-elected every week in mass meetings at each striking university--has declared that de Villepin and his government must go, along with the CPE. They also want the government to lift other laws controlling the conditions of youth employment--for example, a law similar to the CPE passed last August.<p>
The committee's call for an April 11 day of action remained in place as <i>Socialist Worker </i>went to press, along with a call for a national strike on April 18. The high school student union likewise urged students to "maintain the pressure" on the government.<p>
Many student activists last week refused to abide by the grace period granted by the Intersyndicale while it negotiated with the UMP. Their actions included a student sit-in at the main post office in Rennes that was joined by workers in a spontaneous strike that lasted five hours, and the two-hour occupation of another train station in Paris, which blocked international rail traffic.<p>
The French struggle has shaken Europe and set an inspirational example in how the fight against the bosses' neoliberal agenda. Now the question is how the movement will react if the government tries to re-enact the CPE under another name--and how its left wing can broaden and deepen the radicalization to continue the fightback.<p>

