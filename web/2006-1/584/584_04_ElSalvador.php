<font face="Times New Roman, Times, serif" size="5"><b>Return of El Salvador's death squads?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 14, 2006 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">IN THE past few weeks in El Salvador, there have been six assassinations in the Department of Colon. Most of the victims were teenage boys, and one of the bodies was badly mutilated. In each case, witnesses described groups of men dressed all in black, with black ski masks and army boots. <p>
The police took the bodies away without sending any detectives to investigate. They blamed the violence on gang rivalry, but only one of the victims was known to be involved with a gang.<p>
There are eight to 10 homicides every day in El Salvador. Of these, on average, only one is investigated and the rest are blamed on "gangs." People in the communities where the assassinations took place believe, because of the refusal to investigate, that the men in black are not part of a gang but are working with the police. People are afraid to testify because there is no witness protection program for those who can't afford to pay. <p>
Some people in these communities note that all of the assassinations happened within two weeks prior to recent elections. They believe this was an attempt to intimidate their community, which has historically voted in large numbers for the Frente Faribundo Marti para la Liberacion National (FMLN), the main left-wing party. Other election intimidation included threats by Secretary of State Condoleezza Rice to withdraw U.S. aid if the FMLN won in the elections.<p>
For those who are familiar with the history of El Salvador's 12-year-long civil war--in which 80,000 people were killed, most of them by the military and right-wing death squads aligned with the ruling ARENA party--these events feel eerily like deja vu. When a peace agreement was signed in 1992, it included amnesty for people involved in human rights violations. <p>
A few years ago, the government created an elite force called "La Sombra Negra" ("The Black Shadow"), ostensibly to deal with the problem of gangs. When the gangs subsided, this group also faded from the public spotlight. It's unclear whether the assassins are part of La Sombra Negra or another group. It's also unclear how active they are in other parts of the country. <p>
This could be part of the government's new policy known as the "supermanoduro" (the "super-hard hand"), giving police more power to "fight gangs," but nobody knows for sure. All we can say with certainty is that the people are being terrorized.<p>
This is the legacy of a war against the Salvadoran people that had the full backing of U.S. imperialism. The U.S. currently has at least two military bases in El Salvador and is slated to open up a new school here--the International Law Enforcement Agency--which will be similar to the School of the Americas that has trained so many torturers and assassins. We must speak out against these outrages.<br>
<b>Kyle Gilbertson, </b>Chicago<p>

