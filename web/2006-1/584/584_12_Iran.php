<font face="Times New Roman, Times, serif" size="5"><b>U.S. plan for war on Iran?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 14, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">THE BUSH administration is getting ready to use nuclear weapons against Iran, according to investigative journalist Seymour Hersh.<p>
According to an article due to be published in the <i>New Yorker</i> magazine as <i>Socialist Worker</i> went to press, U.S. officials have been involved in "extensive planning" for a possible attack on Iran.<p>
"Current and former American military and intelligence officials said that Air Force planning groups are drawing up lists of targets, and teams of American combat troops have been ordered into Iran, under cover, to collect targeting data and to establish contact with anti-government ethnic-minority groups," Hersh wrote.<p>
According to Hersh, the administration's plans include a goal of identifying and isolating "three dozen, and perhaps more...targets that could be destroyed by precision strikes and short-term commando raids," including sites linked to Iran's alleged nuclear weapons program.<p>
Those "precision strikes" would be carried out using bunker-buster nuclear weapons, according to Hersh--and would include a potential strike against Iran's main centrifuge plant at Natanz, 200 miles south of the capital of Tehran.<p>
As one former senior intelligence official told Hersh, "Every other option, in the view of the nuclear weaponeers, would leave a gap." "'Decisive' is the key word of the Air Force's planning. It's a tough decision. But we made it in Japan," the official told Hersh, referring to the dropping of atomic bombs on Hiroshima and Nagasaki.<p>
No one could miss the hypocrisy of using nuclear weapons strikes against Iran in order to prevent the development of nuclear weapons. But as one high-ranking diplomat told Hersh, Iran's nuclear program--the subject of much heated rhetoric from Bush administration officials--is lower down on the list of reasons for pushing for war.<p>
 "This is much more than a nuclear issue," the diplomat told Hersh. "That's just a rallying point, and there is still time to fix it. But the administration believes it cannot be fixed unless they control the hearts and minds of Iran. The real issue is who is going to control the Middle East and its oil in the next 10 years."<p>
The White House immediately denied Hersh's allegations, claiming that the U.S. is engaged in "normal defense and intelligence" planning when it comes to Iran. Those operations--including simulated nuclear-weapons bombings by Naval tactical aircraft operating from carriers in the Arabian Sea--have reportedly been going on since last summer.<p>
According to Hersh, U.S. officials are banking on the idea that a U.S. attack on Iran might provoke an uprising by Iranians against the hard-line government. Similar arguments were made ahead of the invasion of Iraq, when administration officials predicted U.S. troops would be welcomed as liberators.<p>
Nevertheless, Hersh told CNN last week, "the planning for Iran is going ahead even though Iraq is a mess. I think they really think there's a chance to do something in Iran, perhaps by summer, to get the intelligence on the sites." "The guys on the inside really want to do this," he added.<p>

