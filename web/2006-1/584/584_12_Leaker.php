<font face="Times New Roman, Times, serif" size="4">Bush reaches new heights of hypocrisy</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Leaker-in-chief</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 14, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">"I DON'T know of anybody in my administration who leaked classified information. If somebody did leak classified information, I'd like to know it, and we'll take the appropriate action."<p>
George W. Bush was talking tough in February 2004, when controversy over revelations about the leaked identity of CIA agent Valerie Plame prompted a grand jury investigation targeting administration officials.<p>
Two years later, the investigation has come full circle, casting suspicion on Bush himself. A court document filed last week by special prosecutor Patrick Fitzgerald revealed that Bush personally approved the disclosure of classified information about Iraq's supposed weapons capabilities to a select few reporters.<p>
The disclosure came from grand jury testimony from Lewis "Scooter" Libby, former chief of staff to Dick Cheney. Libby resigned in disgrace last year after being indicted in the CIA leak scandal. According to Fitzgerald's document, Libby testified that Cheney told him the president himself authorized the leaking of parts of a National Intelligence Estimate about Iraqi weapons capacity--as part of an effort to offset criticism of the U.S.-led invasion.<p>
The new disclosure for the first time places Bush directly in a chain of events that led to a meeting where, according to prosecutors, the identity of CIA employee Valerie Plame--then still undercover--was provided to <i>New York Times </i>reporter Judith Miller.<p>
The long-running leak scandal has exposed the cynicism and arrogance of the Bush White House in pursuing its war on Iraq--and the lengths to which the administration was willing to go to smear any opponents.<p>
The aim in outing Plame was to punish Plame's husband, former Ambassador Joseph Wilson. Wilson had traveled to Niger to investigate claims that Iraq was trying to purchase uranium for nuclear weapons. His conclusion that the claim was a fabrication was shoved aside by an administration intent on invading.<p>
When Wilson went public in criticizing the intelligence the administration cited to get its invasion, the Bush administration took aim at him.<p>
According to the new court papers, two days after Wilson's op-ed article appeared in the <i>New York Times</i>, Libby spoke with <i>Times</i> reporter Judith Miller as a confidential source--after Cheney told Libby that Bush authorized Libby "to disclose certain information" in the National Intelligence Estimate. A few days later, Libby spilled the beans to Miller about Plame's identity.<p>
So far, the White House has mainly quibbled with the accusation--arguing that Bush didn't "leak" classified information to the press, but merely declassified it, which he has a right to do, at any time. But that doesn't explain why Bush waited 10 days after his first authorization to Libby to declassify further portions of the entire National Intelligence Estimate--which allowed Libby to feed bits of intelligence to Miller, a reporter who could be counted on to repeat the administration's line.<p>
What's more, the absurdity of Bush authorizing the leak and then pretending outrage over other disclosures to the press hits new heights of hypocrisy. Whether or not Bush, Cheney or Libby did anything technically illegal, the real scandal exposed by this latest revelation is that the Bush administration bolstered its case for war by leaking patently false "intelligence" to a media all too willing to go along for the ride.<p>

