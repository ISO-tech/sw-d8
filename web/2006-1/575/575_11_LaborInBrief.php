<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">February 10, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Sikorsky Aircraft</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jason Poppa</b></font><p>

<font face="Times New Roman, Times, serif" size="3">STRATFORD, Conn.-- Workers from Teamsters Local 1150 at Sikorsky Aircraft are gearing up for a contract battle. With talks to resume February 6, workers are left feeling that a strike may be inevitable. <p>
According to one union shop steward, small workplace disruptions have led to workers being sent home. "People are really talking strike," said Lawrence, a 27-year employee and shop steward. <p>
Sikorsky, which recently received more than $19 billion in defense contracts from the U.S. government, wants workers to pay more toward their annual health care costs. <p>
With increased co-pays, prescription costs and out of pocket maximums, the proposed health care plan could cost workers up to as much as 25 percent of their annual income. Pensions, overtime and outsourcing are also major sources of concessions for Sikorsky in this contract. <p>
The company wants to eliminate overtime pay and force workers work to a swing shift--increasing their hours to 10 and 12 hours a day. According to one rank and filer, "The younger workers are nervous about the vending out of jobs." Union officials have said that they will not accept concessions with this contract.<p>
With company talk of invoking the union-busting Taft-Hartley Act, it appears as though a real battle may be shaping up at Sikorsky Aircraft.<p>

