<font face="Times New Roman, Times, serif" size="5"><b>War crimes sponsored by the U.S.</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 10, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE USE of napalm and chemical weapons, poisoned food supplies, rape, torture--these are some of the atrocities carried out by the Indonesian military during its 24-year occupation of East Timor (or Timor-Leste).<p>
Now, a report from the newly independent country's Commission for Reception, Truth and Reconciliation (known by its Portuguese acronym CAVR) has documented these war crimes--and the U.S. government's support for the carnage with its decades-long military and political backing of the Indonesian government.<p>
The 2,500-page report, titled "Chega!" ("Enough" in Portuguese), shows that some 180,000 East Timorese--approximately a third of the population--were killed or starved to death during the Indonesian military's occupation from 1975 to 1999.<p>
The report--based on thousands of previously secret U.S. documents provided by the Washington-based research institute, the National Security Archive--also documents the role played by the U.S., Australia, Portugal, Britain, France and the United Nations (UN).<p>
The CAVR was created in 2001 by the UN and East Timor to provide a full accounting of abuses during the Indonesian occupation.<p>
Its report states that sexual violence was "widespread and systematic...in which members of the Indonesian security forces openly engaged in rape, sexual torture, sexual slavery and other forms of sexual violence throughout the entire period of the invasion and occupation." This included "keeping lists of local women who could be routinely forced to come to the military post...so that soldiers could rape them. Lists were traded between military units."<p>
The commission delivered its report to East Timor President Xanana Gusmao in October, and while he sent it to parliament in November and to UN Secretary General Kofi Annan last month, the findings have not been widely publicized. The National Security Archive has posted some documents on its Web site, hoping that this will lead to a wider distribution of the report.<p>
The CAVR's findings expose how five U.S. presidencies supported the invasion and occupation of East Timor.<p>
"The Commission finds that the United States of America failed to support the right of the East Timorese people to self-determination, and that its political and military support were fundamental to the Indonesian invasion and occupation," the report said. "The support of the United States was given out of a strategically motivated desire to maintain a good relationship with Indonesia, whose anti-communist regime was seen as an essential bastion against the spread of communism."<p>
The report describes a December 6, 1975, meeting in Jakarta--the day before the invasion of East Timor--in which Indonesian President Suharto asked President Gerald Ford and Secretary of State Henry Kissinger for their "understanding" if his government took "rapid or drastic action" against East Timor.<p>
"[W]e will understand and will not press you on the issue," Ford assured Suharto. "It is important that whatever you do succeeds quickly," said Kissinger, requesting that Suharto wait until he and Ford had returned to the U.S.<p>
Other documents show that, during its invasion, Indonesia used former U.S. Naval ships that had been supplied or sold to Indonesia. After the invasion, "U.S.-supplied weaponry was crucial to Indonesia's capacity to intensify military operations from 1977 in its massive campaigns to destroy the Resistance in which aircraft supplied by the United States played a crucial role," according to the report.<p>
The Carter administration and others that followed helped conceal any evidence that atrocities were taking place in East Timor. <p>
As the CAVR report states, "U.S. administration officials refused to admit that the primary reason that East Timorese were dying in their thousands was the security policies of the Indonesian military." The report also criticizes the UN and the permanent members of the Security Council, which the U.S. dominates, for failing to take effective action to stop the occupation.<p>
The Indonesian government should be held accountable for its war crimes against East Timor--along with its backers in the United States.<p>

