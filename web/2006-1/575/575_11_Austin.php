<font face="Times New Roman, Times, serif" size="5"><b>Austin transit workers settle for mixed deal</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Mike Corwin and Dana Cloud</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 10, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">AUSTIN, Texas--After a prolonged campaign and the threat of a strike, city bus drivers, mechanics and maintenance workers here voted overwhelmingly to accept a new contract. The workers, members of Amalgamated Transit Union Local 1091, had been without a contract since April 2005.<p>
From the beginning of negotiations last year, it was clear that Capital Metro, Austin's transit authority, and private management company StarTran had little interest in bargaining fairly, hiring a high-priced union-busting lawyer and insisting on a two-tier wage scale.<p>
The union took the unprecedented step of staging a one-day strike last September. A subsequent strike authorization vote passed 289-24.<p>
At the beginning of 2006, Capitol Metro unilaterally imposed a new wage scale, knocking down starting pay by $2. The union leadership called for a January 30 strike deadline. The day before the deadline, negotiations broke down and it was announced that a strike was to commence.<p>
However, at the last second union, president Jay Wyatt contacted the mayor and requested that he mediate one final round of negotiations. At 3 a.m. the next morning, it was announced that a deal had been reached and the strike averted.<p>
The contract, which was approved by union members 194-3, contains a number of concessions to management, including a $5 increase in co-pays for doctor visits and prescription drugs. While the two-tier wage scale was scrapped, starting pay was reduced by more than $2 to $11.47 an hour, with a longer period of time to reach the top rate of pay.<p>
The union did win 3 percent pay raises in each year of the contract, retroactive to end of the last contract. Management also agreed to resume the automatic deduction of union dues from members' paychecks, which it canceled after the one-day strike.<p>
Last fall's one-day strike and the threat of a prolonged strike electrified the community and put class issues right into the center of Austin public consciousness. The two-year agreement expires in June 2007, which means that negotiations for the next contract will be starting up again soon, with management sure to press for more takeaways. But the memory of this fight will carry into that campaign.<p>
Travis Cornelius, a Capital Metro bus driver, told <i>Socialist Worker, </i>"They thought they could break our union--and that's what it really was about; it was about union busting. They didn't intimidate us. They didn't bust our union. And we are going to be even stronger. They didn't bust our union, didn't break our spirit, and that's a big deal."<p>

