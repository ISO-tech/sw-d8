<font face="Times New Roman, Times, serif" size="5"><b>Execution halted in Maryland</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Derek Tyner</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 10, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">MARYLAND DEATH row prisoner Vernon Evans won a reprieve hours before he was scheduled to be put to death, in a resounding victory for anti-death penalty activists.<p>
The Maryland Court of Appeals intervened in a fight that pitted Vernon and his supporters against Maryland's pro-death penalty Gov. Robert Ehrlich, who was counting on another execution to kick off his campaign for re-election.<p>
The judges granted Vernon's last-minute appeal, agreeing to hear arguments on several points, including the effectiveness of his trial lawyer, the state's execution procedures and the racial and geographical bias in the state's death penalty system identified in a University of Maryland study commissioned by Ehrlich's predecessor.<p>
Vernon was under death warrant for the week of February 6 and would have become the second inmate in 60 days to be executed in Maryland. But an aggressive grassroots campaign around the case gathered steam in the final weeks, with Vernon and members of his family playing a central role.<p>
After the state issued its death warrant, Evans' relatives joined with activists in Baltimore and Washington, D.C., to publicize their call for clemency and highlight the deep-seated flaws of Maryland's execution system in a series of panels, marches, rallies and speakouts.<p>
Vernon gained local and even national attention, in particular, because of a Web blog created for him last year (<a href="http://www.meetvernon.blogspot.com" target="_blank">www.meetvernon.blogspot.com</a>), on which he answered questions about his case and helped personalize the fate of a prisoner living on death row.<p>
Last week, Evans' lawyers presented Ehrlich with a video petition that, in addition to legal arguments for clemency, included testimony by Evans' extended family about the role he plays in the lives of local students, his own children and other inmates. Through a spokesperson, Ehrlich acknowledged receiving the petition but said he had no intention of reviewing it.<p>
On February 4, more than 100 people came out to Baltimore's Supermax prison to oppose Vernon's execution and tell Ehrlich that the death penalty is racist, anti-poor and cruel and unusual. Marching through a pouring rain, the protesters called for the execution to be halted.<p>
Nihar Bhatt of the International Socialist Organization told the crowd, "To this government, it's not whether you kill--it's who you kill and what you kill for" that determines whether you receive the death penalty.<p>
The intensive campaign for Vernon drew favorable media attention and the support of civic and religious leaders. This growing profile cast attention on both the specifics of Vernon's case and Ehrlich's imperious dismissal of documented racial bias in the use of the death penalty in Maryland.<p>
A 2003 state-commissioned study concluded that prosecutors were more likely to seek and win capital convictions against Black defendants who killed white victims.<p>
Evans' case fits the pattern precisely. He is an African-American man convicted and sentenced to death for the murder of two white motel clerks in Pikesville, Md., in 1983. The trial was based entirely on circumstantial and hearsay evidence, including the testimony of a jailhouse snitch. The testimony of the sole eyewitness, which suggests that Vernon wasn't the shooter, was never heard in the original trial.<p>
At the time of the University of Maryland study, a growing tide of opposition forced then-Gov. Parris Glendening to halt the state's killing machine by imposing a moratorium. Ehrlich lifted the moratorium when he took office, and two executions later, he was brushing aside appeals from state officials and civic leaders, death penalty activists, high-level clergy, as well as the ACLU and the NAACP, to grant clemency to Vernon.<p>
In earning this recent victory, anti-death penalty activists in Maryland showed that grassroots struggle plays a key role in shifting the debate. They not only put pressure on the courts to grant Vernon's appeal, but they generated a climate to push for wider demands, including the reinstatement of the Maryland's moratorium, if not outright abolition.<p>
As Evangelist Gwen Bates, Vernon's sister and a leading participant in the struggle to save his life, said, the verdict felt "like a burden was lifted from us, [but] we're going to continue to fight."<p>
<i>Alex Bennett and Nikita Pion-Klockner contributed to this report</i>. <p>

