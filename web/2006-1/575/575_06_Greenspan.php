<font face="Times New Roman, Times, serif" size="4">Alan Greenspan steps down as Fed Chief</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"The central banker of neoliberalism"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 10, 2006 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">WHEN ALAN Greenspan stepped down last week after nearly two decades as the country's most important financial official, the media's praise was almost universal. Since 1987, Greenspan was chair of the Federal Reserve--the government's central bank and probably the most important institution in setting economic and fiscal policy.<p>
During the boom years of the 1990s, Greenspan was treated as a genius responsible for everything good in the economy. His "steady hand," Bill Clinton later said, "minimized the bad times and let the good times roll."<p>
Since the recession at the beginning of this decade, the sluggish economy tarnished Greenspan's reputation a little, but he was still treated with reverence upon his retirement.<p>
Greenspan's real accomplishments are different, as JOEL GEIER, associate editor of the <i>International Socialist Review</i>, explains in this interview.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>DOES GREENSPAN deserve the praise he's gotten as the bringer of prosperity and economic stability?</b><p>

OF COURSE not. What is true is that he's the most important central banker of the postwar period. He has to be seen as the central banker of neoliberalism--the person who enabled many of the policies from the era of neoliberalism to be introduced and implemented. <p>
One of the most important things about the whole period when he was the central person in terms of economic policy has been the enormous shift in wealth from working people to capital. This growing inequality was something he presided over--and was, in fact, a spokesperson for.<p>
One of the more recent examples of how was in the late 1990s, when the government was running surpluses. Greenspan testified to Congress that the budget surpluses the government was running were a threat to the stability of the system, and therefore, Bush's tax cuts, which favored the rich, should go through. <p>
Then, when the tax cuts produced the enormous deficits of the last few years, instead of speaking out against his previous testimony, he came in saying that the budget had to be balanced by cutting social programs, in particular, Social Security and Medicare.<p>

<b>THERE ARE some critics, even in the mainstream, who say Greenspan's policies contributed to financial instability and other factors that threaten the U.S. economy.</b><p>

GREENSPAN'S HIGH stature is primarily due to the fact that in the late 1990s, when the Asian financial crisis was starting to sweep throughout the world, Greenspan was capable of stopping the United States from going immediately into recession.<p>
Under his monetary policy, interest rates were cut drastically, and large amounts of money was pumped into the system, which stopped the crisis from hitting the U.S. and overcame the effect of the meltdown of Russia, the bankruptcy of Long-Term Capital Management and so on.<p>
But Greenspan's policy of pumping money into the system had the unintended consequence of leading to an enormous investment bubble in the stock market--money poured into Wall Street, inflating stock prices, in particular among high-tech companies. <p>
The stock market bubble started deflating from its peak in early 2000. When the recession did take place shortly afterward, Greenspan then came up with another easy-money policy. He cut interest rates, eventually to 1 percent--which means they were effectively negative interest rates, since they were lower than the rate of inflation. This produced a huge bubble in housing prices that in turn has caused an imbalance throughout the world. <p>
As a result of neoliberal policies, workers' wages are lower, and family income is down considerably after inflation. In Illinois, for example, household income dropped more than $6,000 between 1999 and 2004.<p>
People have made up the decline in their living standards by borrowing against rising housing prices, through home equity loans and so on. They kept up consumer spending despite the decline in income, and this has caused an enormous trade deficit. <p>
This is a direct result of the policies Greenspan initiated during the Asian crisis--of making the United States the importer of last resort. The trade deficit is now over $700 billion a year, and the United States is an enormous debtor, dependent on foreign capital. <p>
So you have a housing bubble and an enormous trade deficit that are legacies of Greenspan's monetary policies. Together, they have the possibility of making the next downturn in the economy a much deeper and more serious recession than anything we've seen in the postwar period. <p>
This is why there's a big question hanging over the accolades that Greenspan has gotten--because the policies he's instituted have produced these imbalances throughout the entire world, which threaten the stability of the system in the next downturn.<p>

<b>AS GREENSPAN resigned, the federal government announced that economic growth slowed to a crawl in the last three months of last year. What does this mean for the prospects of the economy and the new Fed chief?</b><p>

THERE HAS been a slowdown in the economy, some of it due to the fact that the Fed has had to raise interest rates to try to prevent the housing bubble and the trade balance from getting even more exacerbated. You also have the effect of high gas and oil prices going through the system. <p>
The Fed is afraid that inflation--which has been contained to a large degree by the rise of China and India as producers of cheap exports, and by the holding down of wages in this country--could get out of control again. So it is raising interest rates again, and this has led to the fear that we may get a new period of "stagflation"--a period of rising interest rates and lower growth rates. <p>
It's still early to say whether this is in the cards. But the consequences of the years of keeping the economy going through low interest rates and through these stock market and housing bubbles are now starting to show up in the American economy. <p>

