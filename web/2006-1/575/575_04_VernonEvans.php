<font face="Times New Roman, Times, serif" size="4">Maryland governor won't face execution victims</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Vernon Evans' struggle</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 10, 2006 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">VERNON EVANS, a Maryland death row prisoner, won a last-minute stay only hours before he was scheduled to be put to death, as <i>Socialist Worker </i>went to press. MIKE STARK of the Campaign to End the Death Penalty in Washington, D.C., wrote the following article about the struggle to save Vernon.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">IT CAME as no surprise to family members of Maryland death row prisoner Vernon Evans that when they went calling on Gov. Robert Ehrlich he wasn't even home. <p>
Why should he be? He wasn't home when Davida Oken, the mother of Steven Oken, came in July 2004 desperately looking for mercy for her son. And he wasn't home just last December for Delores Williams, the mother of Wesley Baker. <p>
But this time, when the Evans family came, they came prepared--with over 60 supporters, banners, placards, an accompaniment of television cameras and a video recording of their request for clemency. An incredible document, it illustrates in vivid detail the devastating impact an execution has on the family of the executed. <p>
In the video, you see the Evans family battling for its own survival as they fight to save their son, brother, father and grandfather. Speaking directly to the governor, the Evans family demonstrates a poise and clarity that both affirms their dignity and condemns the brutal system they face. <p>
This compelling document was inspired by Vernon himself, a vocal spokesperson against the death penalty. He is the author of the <a href="http://www.meetvernon.blogspot.com" target="_blank">first ever death row blog</a>, has led a seminar on the death penalty at Maryland's Mount St. Mary's College and has participated via speakerphone in abolitionist rallies across the state.<p>
Vernon Evans was facing execution during the week of February 6. He had exhausted every appeal, and it appeared that his lawyers would not be able to win relief in the courts. Up until Monday, the courts had turned a deaf ear to compelling evidence suggesting Vernon Evans was not the shooter in the crime. <p>
No jury ever heard the testimony of the only eyewitness to the crime, who stated Vernon was not the shooter. Further, a 2003 study commissioned by the governor's office showed Maryland's death penalty is applied in an arbitrary and racially biased manner. Vernon, a Black man accused of killing two white victims, fits this disturbing pattern.<p>
Which led Vernon's family to Robert Ehrlich's front door. There used to be a time when Maryland governors, weighing the awesome power of clemency in a death penalty case, would personally visit the condemned man and look him in the eye. This display was undoubtedly more a show of machismo than a deliberate exercise in justice.<p>
But Governor Ehrlich, the law-and-order Republican, doesn't have the stomach for the mothers of death row prisoners. The former Princeton boxer uses his skills to bob and weave away from the reality of executions and instead sends out his friends from the Baltimore County State's Attorneys office to scream indignantly at the audacity of Vernon's family. Baltimore County Deputy State's Attorney Stephen Bailey called the video "a blatant attempt to manipulate Governor Ehrlich based on emotion rather than facts or sound judgment." <p>
In a state where prosecutors have fine tuned victim impact statements to ensure they traumatize juries so much they will approve of state death, these protestations are a sick hypocrisy. Meanwhile, the governor's counsel has publicly stated he wasn't sure if the governor would even take the time to view the video.<p>
In fact, Ehrlich, when cameras finally caught up with him on the Saturday the Evans family came calling, was found at the annual Maryland State Police Polar Bear Plunge. There, he seemed nervous and disturbed at the mere mention of Vernon Evans. He stammered about his commitment to the process and that he would "review all the facts."<p>
Ehrlich is committed to the "process" because he is confident of its outcome--a quiet, antiseptic and well-rehearsed death for Vernon Evans. <p>
Vernon and his family, however, decided not to go quietly and made sure, whether he's home or not, that the governor got the message. Their efforts helped build the pressure that won the stay for Vernon.<p>

