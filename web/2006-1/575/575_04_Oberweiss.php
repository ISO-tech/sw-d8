<font face="Times New Roman, Times, serif" size="5"><b>Exploiting immigrant workers</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 10, 2006 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">AS DAIRY magnate Jim Oberweis makes a bid for Illinois governor, it is no surprise to see him plying the same anti-immigrant rhetoric he used during last year's senate race. But as a recent Illinois Department of Labor investigation of his dairy highlights, he is exploiting the immigrant community twice over.<p>
Rosa Ramirez and Jorge Ibarra, two undocumented immigrants living and working in the northwest suburbs of Chicago, were employed to clean three Oberweis Dairy stores for $3.25 an hour--less than half of the state minimum wage. <p>
In November, the Illinois Coalition for Immigrant and Refugee Rights (ICIRR) and the Chicago Workers' Collaborative filed the complaint on the workers' behalf detailing two specific violations: that the company failed to pay the workers the agreed upon amount, and that this amount was below minimum wage.<p>
Shortly after the workers came forward, a local chapter of the Minuteman Project, the racist vigilante group that has raised havoc among the immigrant communities most dramatically with their violent action at the U.S. borders, called for their deportation. At present, there is no action being taken against the two workers. <p>
This case is an unfortunately common situation that confronts what ICIRR estimates to be roughly 500,000 undocumented immigrants in Illinois, where they are exploited by employers and threatened with deportation should they make a complaint. <p>
The Oberweis case is part of another disturbing trend as well. Ramirez and Ibarra were hired as subcontractors through Patmar Janitorial Services, in a manner that "defers responsibility from the employer, essentially, allowing employers to wash their hands of any violation," according to Tim Bell of the Chicago Workers' Collaborative. <p>
In a press release, Bell stated: "The exploitation of any worker hurts all workers. If a company is paying workers $3.23 an hour--that's what happened in this case--it is lowering the bar. We need better enforcement of what these companies are doing." He adds to this, "You know, the Minutemen are workers, too, it is just that they think Oberweis speaks to their interests." <p>
Oberweis, whose net worth is estimated between $7.6 million and $33.4 million, has used undocumented immigrants as a scapegoat for the nation's economic problems throughout his campaigns. <p>
In one advertisement, he circled Chicago's Soldier Field in a helicopter claiming that "10,000 illegal aliens a day" were crossing into the U.S., so many that they could fill the 61,500-seat stadium with "new batch of them every week." In the ad, he went on to sneer that immigrants are taking American worker's jobs and demanding government assistance. <p>
As Bell points out: "This case clearly shows that Oberweis' interest in attacking immigrants stems not from his stated desire to protect the jobs of native-born workers; rather it is to stir up anti-immigrant sentiments and policies to terrorize his own undocumented employees so that they will not complain when mistreated, cheated, and disrespected. What he did not count on was the bravery of Rosa and Jorge to bring to light his true motives."<br>
<b>Josh Gryniewicz</b>, Chicago<p>

