<font face="Times New Roman, Times, serif" size="4"><b>Stand up for gay rights</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Ellie Fingerman</b></font> <p>

<font face="Times New Roman, Times, serif" size="3">AN IMPORTANT victory was won in Washington state after 30 years of waiting. In a 25-23 vote, the state legislature passed a civil rights bill for gay, lesbian, bisexual and transgendered (LGBT) people, joining 16 other states in outlawing discrimination against people based upon their sexual orientation or gender identity.<p>
Declaring January 23 "Equality Day," the Religious Coalition for Equality (RCE) and Equal Rights Washington (ERW) called a rally that brought out 2,000 people from around the state to the steps of the capitol. "We cannot be satisfied so long as some of our brothers and sisters live in fear of losing their jobs or of losing their homes," said Rep. Ed Murray, the sponsor of the bill and one of four openly gay legislators in the state. "We cannot be satisfied as long as our families are threatened and our relationships do not have the recognition that they deserve."<p>
Other speakers drew the parallel between the fight for civil rights for Blacks in the 1960s and the fight for LGBT rights today. "We have talked long enough about ending discrimination, bigotry and bias in our country and in our state," according to James Kelly, president and CEO of the Urban League of Metropolitan Seattle. "Now is time to write a new chapter in this law of equal protection and equal opportunity for all."<p>
The passing of the civil rights bill was significant not only because of its immediate effect of providing better protection, but also because it provides clear evidence that other victories in the ongoing struggles for LGBT rights--including the fight for same-sex marriage--are possible.<p>
Any day now, the state Supreme Court could hand down a decision that could make Washington the second state in the country to legalize same-sex marriage. LGBT groups have called for either a protest or a celebration at 5:30 p.m. at the Seattle First Baptist Church on the day the decision is handed down.<p>
While this is an important event, it is also crucial to hold a rally in advance of that decision, to build on the momentum of the civil rights bill--and to let the justices know that people in Washington want the legalization of same-sex marriage. A march and rally will be held in Seattle at noon on March 11 to mark the one-year anniversary of the state supreme court hearing the marriage equality case.<p>
<i>For information on these or other actions, please visit <a href="http://www.MarriageEqualityNow.org" target="_blank">www.MarriageEqualityNow.org</a></i>.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Heating assistance</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Sam Bernstein and Leela Yellesetty </b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW HAVEN, Conn.--Activists are circulating petitions to press city officials to accept reduced-cost heating oil from Venezuela.<p>
Last week, the <i>New Haven Register</i> reported that local New Haven politicians are currently looking into requesting reduced-cost heating oil from Venezuela. Venezuela has already struck similar deals with the Bronx and Boston, while Chicago authorities turned down such offers. Rhode Island and Pennsylvania are also currently exploring deals with CITGO, the Venezuelan state oil company.<p>
Activists also plan to hold a press conference in front of city hall to demand that the heating oil is accepted, and that the program be extended to all of Connecticut. <p>
While Connecticut taxpayers have already contributed $5.8 billion to the war in Iraq, the U.S. government has done nothing to address the crisis in heating costs here and across the country.<p>
In New Haven, the idea was initiated by the city director of elderly services, who has met with Venezuelan officials. The New Haven Board of Alders' Black and Hispanic Caucus has also been in consultation with the Venezuelan embassy.<p>
Plans to obtain low-cost heating oil for impoverished New Haven residents could not come sooner. In Connecticut, home heating oil prices have increased by a full 25 percent each year for the past two years. At the same time, the state experienced the second largest growth in income inequality in the entire country, with low-income families earning slightly less than they did in 1991, while the rich saw a 31 percent increase.<p>
While some politicians are opposed to the proposal, people in New Haven are overwhelmingly in support. A victory on this issue could provide some much-needed relief, while raising confidence for future struggles in the state.<p>

