<font face="Times New Roman, Times, serif" size="5"><b>Darwin and his revolutionary contributions</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Ben Davis</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 10, 2006 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">"Darwin," at the American Museum of Natural History in New York City until May 29, with an upcoming national tour. For information, go to <a href="http://www.amnh.org/exhibitions/darwin/" target="_blank">www.amnh.org/exhibitions/darwin/</a></font><p>

<font face="Times New Roman, Times, serif" size="3">ADVOCATES OF intelligent design--a philosophy that claims that there are "scientific gaps" in the Darwinian theory of evolution that can only be explained if life is the product of an "intelligent" creator--often repeat that they just want students to hear all sides of the story.<p>
Echoing them, President Bush has said that he thinks schools should teach a "balanced" curriculum, showing evidence for both theories. On closer inspection, however, this talk about teaching "both sides" is disingenuous.<p>
The Seattle-based Discovery Institute, the "non-partisan" think tank behind the term "intelligent design," has even published a document titled the "Wedge Strategy," arguing that efforts to "teach the controversy" are just the first step in the group's long-term program to defeat "scientific materialism" and advance "the proposition that human beings are created in the image of God," the ultimate goal being to "see design theory permeate our religious, cultural, moral, and political life."<p>
In this context, the "Darwin" exhibition--on view at the American Museum of Natural History in New York until May 29 before traveling to Boston, Chicago, Toronto and London--is welcome for the way that it clearly cuts through this fake debate.<p>
The show brings together historical documents, artifacts and personal effects from the life of the British naturalist Charles Darwin (1809-1882), along with educational materials, videos and interactive exhibits that illustrate the legacy of his thought for today. The first galleries focus on the young Darwin, showing how the theories for which he became famous were not arrived at dogmatically, but were the product of painstaking observation about the natural world.<p>
In fact, Darwin was set to enter the clergy before joining the crew of the <i>H.M.S. Beagle </i>as ship's naturalist on a voyage to the Americas. It was only after logging thousands of observations about different species over the five years of this trip--and then carefully reviewing the data at home in England for many more years--that Darwin was able to argue that natural selection was the best explanation for the development of life.<p>
The most impressive thing about this show is the way it tackles the present conservative attack on Darwin. The penultimate gallery, titled "Evolution Today," points out that religious objections to evolution are as old as the theory itself.<p>
A video called "Scientists on Faith" features, among others, Dr. Francis Collins, director of the Human Genome Project. Though Dr. Collins admits to believing in a "personal god," he also insists that faith cannot be ground for a "scientific controversy"--intelligent design seeks to explain facts through a cause that is by definition unknowable, thus violating science's fundamental animating principle.<p>
If there is a criticism of the exhibition, it would be that it doesn't focus enough on legitimate scientific advances on Darwin's theory, showing how ideas of evolution have been expanded to fit new genetic facts about life--a process that we can expect to continue as knowledge advances.<p>
Nevertheless, it does make a clear, accessible statement that, as one video puts it, "[e]volution serves as the foundation for all of modern biology, including research critical to human welfare, medicine and the fight against disease." Teaching the "controversy" over intelligent design amounts to shutting down useful scientific debate, not advancing it.<p>

