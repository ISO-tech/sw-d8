<p><a name="ROTC"></a><font face="Times New Roman, Times, serif" size="4"><b>Kick ROTC off campus</b></font><p>

<font face="Times New Roman, Times, serif" size="3">DURING THE recent conformation hearings for Judge Samuel Alito, the Democrats attempted to portray themselves as opposing Alito through their questioning about the right-wing group Concerned Alumni of Princeton (CAP).<p>
When asked about the group, Alito stated he could not recall being a member. However, he did mention that he was angry that the ROTC had been removed from the Princeton campus during the Vietnam war era. He said that he felt it was wrong for the ROTC to have been removed and that he believed that one issue CAP believed in and fought for was to have the ROTC on campus. <p>
If the Democrats had a backbone or were concerned about racism, sexism, homophobia or imperialism, they might have questioned Alito about why he believed the ROTC had a right to be on campus. <p>
The ROTC violates universities' discrimination policies. The military serves as the armed wing of big business. It actively promotes racism and sexism, and the "don't ask, don't tell," policy violates gay rights. To top it off the military suppresses popular movements, as it did during the Vietnam war and in non-white neighborhoods and universities.<p>
Alito showed his true colors and the Democrats showed they have no concern about racism, sexism, homophobia or imperialism. <p>
After the approval of Alito, he will arrive on the Supreme Court just in time to watch as the ruling is handed down in <i>FAIR v. Rumsfeld</i>, a case challenging the constitutionality of the Solomon Amendment, which forces schools that receive federal funding to allow military recruiters onto campus. <p>
It is time to form a movement to drive the ROTC off of our campuses--just like what occurred at Princeton when Alito was a student.<br>
<b>Brian Carey,</b> Madison, Wis.<p>

