<p><a name="Faith"></a><font face="Times New Roman, Times, serif" size="4"><b>A faith-based scandal</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AT THE House of Refuge in Salt Lake City, Utah, "rehabilitation" apparently includes forced labor. According to recent news reports, the substance-abuse rehabilitation program, run by the Central Christ Church, is currently under investigation by the Utah Department of Human Services. <p>
Investigators say that the men in the program--who were ordered into it by judges or state agencies--were forced to work as telemarketers for 28 cents an hour. If they didn't agree to do the work, they were threatened with being thrown back in jail.<p>
The story gets worse, however. Even the paltry 28 cents an hour that the men were supposed to receive for working reportedly was withheld and then "donated" to the church.<p>
While Utah officials are likely to pass this off as simply an isolated case of abuse of the system, it is symptomatic of a much wider problem. Namely, that in a society that wastes billions of dollars each year on incarcerating more than 2 million people--a large percentage for non-violent drug offenses--actual treatment and rehabilitation for substance abuse is almost always at the very bottom of the list of priorities.<p>
Under the Bush administration, the problem has only gotten worse, with the push on both the state and federal level to shift the costs of social service programs--including drug and alcohol treatment--away from the state and towards private enterprises which are often "faith-based." <p>
At House of Refuge, for example, treatment "rehabilitation" includes mandatory Bible study, weekly church attendance, and the reading of 30 chapters of the Bible each week. And state officials in Utah got behind House of Refuge in a big way--helping the program apply for government contracts, Housing and Urban Development block grants and private-sector funding as part of the Bush administration's faith-based initiative program.<p>
Perhaps the Central Christ Church will find a "biblical" justification for their actions--something, maybe, about hard work soothing the soul. But in a sane and just society, forcing people with substance abuse problems to work for pennies could never be passed off as "treatment."<br>
<b>Nicole Colson</b>, Chicago<p>

