<font face="Times New Roman, Times, serif" size="4"><b>Carter wasn't a lesser evil</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I GREATLY appreciated Paul D'Amato's article "Beware Wolves in Democrat Clothing" (January 20). I especially liked how he exposed the hypocrisy of Jimmy Carter. I have often felt that Carter did more damage to this country than any other president. <p>
It was Carter who started rolling back the political and social gains of the 1960s. With his pious speeches about how "the government can't do everything" and "life is unfair," as well as his cynical exploitation of the Iran hostage non-crisis, he pulled the political climate to the right. <p>
Consider the fact that when Ronald Reagan ran for the presidency in 1976, he was widely dismissed as a right-wing crank. After four years of Carter, Reagan's views seemed "mainstream." Those who think the Democrats are a "lesser evil" would do well to consider this.<br>
<b>Evan Kornfeld,</b> Los Angeles<p>

