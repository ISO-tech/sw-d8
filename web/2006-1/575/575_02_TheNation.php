<font face="Times New Roman, Times, serif" size="5"><b>Protests over racist ad in the <i>Nation</i></b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 10, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">READERS OF the liberal <i>Nation </i>magazine found that the January 9/16 issue carried a racist advertisement denying the existence of the Palestinian people.<p>
The ad, placed by the group Facts and Logic About the Middle East (FLAME), also made absurd accusations meant to demean the Palestinian cause--asserting, for example, that during the Second World War, "the Palestinian Arabs were in Berlin hatching plans with Adolf Hitler for world conquest and how to kill all the Jews."<p>
The ad ends by dismissing as a myth the idea that Israel occupies any Palestinian territory--and says that it is "a shame that the world has accepted" most of the "web of lies and myths that the Arab propaganda machine has created."<p>
The American-Arab Anti-Discrimination Committee (ADC) launched a campaign to demand that the <i>Nation </i>refuse to print such racist and demeaning ads in the future.<p>
"While we strongly respect the right to free speech, ADC believes that the ad, which presents an incredibly skewed and racist perspective of Palestinians and further reinforces stereotypes of Arabs as habituated to violence, constitutes hate speech," reads an ADC press release. "[C]onsidering the continuing problems with hate crimes facing the Arab-American community, the <i>Nation </i>also has a responsibility to its readers not to print ads that are racially offensive, and which may contribute to the worsening problem of racism and prejudice towards any ethnic group or minority, including Palestinians."<p>
The <i>Nation </i>has so far defended its decision to run the ad, saying in its January 23 issue that its policy is to reject "commercial" ads that are "false, lurid or patently fraudulent," but that ads like the one from FLAME that present a political point of view "fall under our editorial commitment to freedom of speech."<p>
In 2004, however, the <i>Nation </i>refused to run ads sponsored by the anti-Semitic Institute for Historical Review, which denies that the Holocaust took place--saying that such arguments are "patently fraudulent."<p>
At a time when Arabs and Muslims around the world have expressed anger at racist and demeaning media depictions, it's a travesty that a supposedly progressive publication like the <i>Nation </i>would defend its decision to publish this bigoted pack of lies.<p>
