<font face="Times New Roman, Times, serif" size="5"><b>A new assault on abortion rights</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 10, 2006 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THE WAR on a woman's right to choose abortion is escalating. In the wake of the Bush administration's cakewalk confirmation of Samuel Alito to the Supreme Court, lawmakers in Georgia, Indiana, Ohio, South Dakota and Tennessee are proposing sweeping new measures that would outlaw all abortions, except when a woman's life is in danger.<p>
The proposals are specifically designed to set the stage for a Supreme Court challenge to <i>Roe v. Wade</i>, the 1973 ruling that legalized abortion. But even before that, the Supreme Court is likely to hear a new challenge to <i>Roe</i>--this time, deciding whether the 2003 federal ban on a late-term abortion procedure, misnamed "partial-birth" abortion by the right wing, should stand.<p>
Today, all 50 states have anti-abortion legislation either on the books or pending, and a host of restrictions--parental consent and notification laws; mandatory waiting periods and "counseling" requirements; even measures mandating building specifications and increased health inspections for clinics--have gutted access in many states.<p>
According to Nancy Keenan, president of NARAL Pro-Choice America, legal abortion itself may be doomed. "I think <i>Roe </i>in the short term will be dismantled," she told Reuters. "We have an anti-choice president, an anti-choice Congress, and now...with the confirmation of Judge Alito to the Supreme Court, we are seeing the potential for a very right-leaning, anti-choice Supreme Court."<p>
Keenan is right about what's at stake today, but the fault doesn't lie only with right-wing politicians and the courts. It is shared by mainstream pro-choice groups that for years focused on lobbying and electing Democrats, rather than building a grassroots movement similar to the one that won abortion rights in the first place.<p>
One million people turned out for the 2004 March for Women's Lives--but march organizers repeatedly sent the message that the Democrats could "save" the Supreme Court and protect abortion rights.<p>
Instead, as we recently saw, the Democrats refused to do anything to try to block Alito's confirmation. From Hillary Clinton labeling abortion as "sad" and "tragic," to John Kerry sponsoring legislation that would legally allow pharmacists to refuse to dispense birth control, to the anti-choice Senate Minority Leader Harry Reid, it's hard to find a single Democrat in Washington willing to stand up unapologetically for abortion rights.<p>
And next month, the Democrats will sink even lower--as Rep. Tim Ryan (D-Ohio) and Sen. Ben Nelson (D-Neb.) are expected to sponsor legislation that aims to reduce abortions by 95 percent over 10 years.<p>
As <i>Nation</i> columnist Katha Pollit put it, "Do you think abortion is tragic and terrible and wrong, that <i>Roe v. Wade</i> went too far, and that the pro-choice movement is elitist, unfeeling, overbearing, overreaching and quite possibly dead? In the current debate over abortion, that makes you a pro-choicer. As the nation marked the 33rd anniversary of <i>Roe</i>, it was difficult to find anyone who will say a good word in public for abortion rights, let alone for abortion itself."<p>
The Democrats have shown that they aren't going to fight for our rights. That's why we can't wait to build the struggle to keep abortion legal. <p>
That struggle will have to built step by step--starting with meeting and forums that begin to re-win the case for the right to choose abortion as essential to women's rights, as well as pickets and protest that confront the anti-abortionists, including when they show up to harass women at clinics. It's time to rebuild the struggle for abortion rights, without apologies.<p>

