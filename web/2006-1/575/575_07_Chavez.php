<font face="Times New Roman, Times, serif" size="5"><b>The real reason the U.S. hates Ch&aacute;vez</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 10, 2006 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">VENEZUELAN PRESIDENT Hugo Ch&aacute;vez has been on the U.S.'s target list since he survived a U.S.-sponsored coup in 2002 and a 2003-2004 bosses' strike aimed at sabotaging the oil industry.<p>
Now comes the latest blast from Defense Secretary Donald Rumsfeld, who said of Ch&aacute;vez: "He's a person who was elected legally--just as Adolf Hitler was elected legally--and then consolidated power and now is, of course, working closely with [Cuban President] Fidel Castro and [newly elected Bolivian President] Mr. Morales and others."<p>
Less headline-grabbing, but perhaps more ominous, was the congressional testimony of Intelligence Czar John Negroponte, who said, "President Chavez, if he wins reelection later this year, appears ready to use his control of the legislature and other institutions to continue to stifle the opposition, reduce press freedom and entrench himself through measures that are technically legal, but which nonetheless constrict democracy."<p>
The rhetoric from Rumsfeld and Negroponte hit the airwaves at the same time that Venezuela accused a U.S. military attach&eacute; of spying and expelled him from the country. The U.S. expelled a senior Venezuelan diplomat in response.<p>
When U.S. officials start to compare foreign leaders to Hitler and liken their countries to dictatorships, that's a sign of their attempt to soften U.S. public opinion for increased U.S. meddling in their affairs.<p>
But is there any truth to these charges? About five minutes of research would show that the opposite is the case.<p>
No one--except the discredited elite opposition that the U.S. backs--disputes that Ch&aacute;vez was freely elected, and that he overwhelming defeated a 2004 attempt to recall him. Independent election monitors from the Carter Center and the Organization of American States verified both elections.<p>
Ironically, one of the opposition's chief complaints with the elections was that the use of electronic voting machines made them prone to tampering--a complaint that the Republican Party in Florida, Ohio and Georgia usually dismisses.<p>
The opposition, knowing that it would lose overwhelmingly in a fair vote, and with prodding from the U.S., pulled out of December's parliamentary elections.<p>
Moreover, it has to be a strange dictatorship where the opposition owns the majority of the media and uses it to campaign against the government.<p>
The opposition organizes openly, despite the fact that many of its leading figures were associated with the 2002 military coup that abolished democracy in Venezuela before it was defeated and Ch&aacute;vez was returned to power. On the weekend before more than 80,000 arrived in Caracas for the World Social Forum, the opposition staged a demonstration that some estimated to involve up to 100,000 people.<p>
If Ch&aacute;vez has become an irritant to the U.S., it's because he has attempted to direct the country's oil wealth to programs for health, economic development and literacy that serve millions of poor people. This is a challenge to the so-called Washington Consensus that has led many governments to abandon these kinds of programs. The U.S. also does not like the fact that its third-largest supplier of oil and gas is not willing to knuckle under to Washington's dictates.<p>
Ch&aacute;vez calls his economic and social programs "21st century socialism," but socialists in Venezuela note that the government is actually quite cautious.<p>
Its land reform program has distributed only a small amount of land that its owners abandoned. And Ch&aacute;vez has said, "We're not proposing to eliminate private property, as the communists do. No. We're not going there."<p>
Ch&aacute;vez and his political supporters handpick candidates for parliament. They have maneuvered to support a moderate leadership in the new trade union federation, the National Workers Union. A circuit court judge recently imposed a gag order barring the press from reporting particular details about a high-profile investigation into the 2004 assassination of a state prosecutor.<p>
The important point about these breaches of democracy is that Venezuelan human rights and trade union activists are taking them on.<p>
The millions of ordinary people who have been mobilized in new trade unions and community organizations are the real source of democracy in Venezuela. They deserve our solidarity--not lectures from the likes of Rumsfeld and Negroponte.<p>

