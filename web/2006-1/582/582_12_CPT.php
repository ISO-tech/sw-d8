<font face="Times New Roman, Times, serif" size="4">Activists freed in Iraq</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Continuing the struggle against war</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 31, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">FRIENDS AND supporters celebrated the release March 23 of three Christian Peacemakers Team (CPT) members who were kidnapped in late November in Iraq.<p>
While the press piled praise on a "flawless rescue" operation by U.S. and British special forces, one of the men who had been held, Norman Kember, reminded reporters why he was in Iraq.<p>
"I do not believe that a lasting peace is achieved by armed force, but I pay tribute to [the soldiers'] courage and thank those who played a part in my rescue," Kember said. "There's a real sense in which you are interviewing the wrong person. It's the ordinary people of Iraq you should be talking to--the people who have suffered so much over many years and still await the stable and just society they deserve."<p>
CPT delegations have been traveling to Iraq since October 2002 to oppose the U.S. invasion and document the horrors faced by Iraqis living under occupation, including interviewing Iraqis detained in U.S.-run prisons and detention centers.<p>
When four CPT members were taken hostage November 26, numerous Muslim and Arab groups around the world voiced their public support for the human-rights activists and called for their release. "We were certainly heartened by the support of Muslim clerics, especially in the Middle East and over all the world," Claire Evans, the CPT's delegation coordinator in Chicago, said in an interview.<p>
On March 10, one of the kidnapped men, American Tom Fox, was found dead in Baghdad.<p>
Despite the danger, CPT members remain dedicated to their cause. "I was on a two-week delegation to Iraq in September, which was formatted very similarly to the November delegation that the four men were on," Evans said. "We really met with a range of people in Iraq, from high-level Muslim clerics both Sunni and Shiite, to Christians, people on the ground, and members of Iraqi human rights organizations...<p>
"We've had over 100 people on our delegations coming back with the voices of Iraqis on the ground directly affected by the violence. We thought that this is a valuable thing to be doing, which can help, we think, lead to a more compassionate government policy on our part by having people pressuring our government to listen to those voices that we heard.<p>
"Our team has been one of the few foreign NGOs, if not the only one, outside of the heavily fortified Green Zone. People in the Green Zone can't get out. A lot of times they're prohibited by their agencies from getting out and talking with regular people on the street. So we thought being able to do that was a very valuable thing."<p>
Evans says that the CPT has received many messages of support, but others claim that the CPT is "unnecessarily putting lives at risk." But as Evans makes clear, "The coalition forces have certainly put a lot more lives in jeopardy that our men have."<p>
It's up to antiwar activists to continue to expose the brutal conditions of life under occupation--and oppose it every step of the way.<p>

