<font face="Times New Roman, Times, serif" size="4"><b>Pushing aside victims of asbestos</b></font><p>

<font face="Times New Roman, Times, serif" size="3">GIVEN THE choice between profits and workers' lives, Congress picked corporate interests again recently with a decision to ax a bill that would set aside money for the victims of asbestos poisoning. Every year, about 10,000 people die from exposure to asbestos, usually coming into contact with the building material at work.<p>
The legislation, which went down in a close non-partisan 58-to-41 vote, would have created a $140 billion fund, what would have been a small start to aiding workers who suffer from the horrible breathing illnesses and cancer as a result of working with asbestos. The <i>New York Times </i>reported that it was defeated by a combination of conservatives who opposed another so-called "entitlements" and liberals who said that the bill fell short of what was necessary.<p>
The legislation did fall far short of what would be needed to make a difference in the lives of those poisoned by this material--but this misses the point. Rather than fighting for real legislation that might make the lives of workers better, Congress's pathetic asbestos bill disappeared as quietly as it entered the scene. Workers deserve better.<br>
<b>Todd Lester,</b> St. Louis<p>

