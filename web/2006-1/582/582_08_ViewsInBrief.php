<p><a name="Iowa"></a><font face="Times New Roman, Times, serif" size="4"><b>No death row in Iowa</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SOME MEMBERS of our state legislature seem all fired up about reinstating the death penalty in Iowa. Since states and nations that decline to execute people have lower violent-crime rates than those that do execute, one might wonder why. I guess these folks just like the idea of killing people--well, only people that really have it coming and only for very good reasons. <p>
Given humanity's less than perfect nature, these enthusiasts ought to be able to count on a plentiful supply of good reasons being always provided by someone--it will sort of make their day. Hum, but I wonder about that approaching day when it is time for each of the rest of us to go. As death-penalty advocates lie there on their deathbed, will they draw any comfort from the love and forgiveness that they have in life been able to extend to and receive from others, or will they wish that they had been able to spend more time at the gallows?<br>
<b>Sam Osborne,</b> West Branch, Iowa<p>

