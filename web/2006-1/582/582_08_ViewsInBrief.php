<p><a name="War"></a><font face="Times New Roman, Times, serif" size="4"><b>Breakfast with a war criminal</b></font><p>

<font face="Times New Roman, Times, serif" size="3">"IN OUR opinion, citizenship and patriotism is something we try to instill in our scouting program. Oliver North is an example of those qualities."<p>
These are the words of Tim Harper of the Boy Scouts of North Carolina, on why they chose Oliver North to speak at their, "Breakfast with Col. Oliver North" fundraiser.<p>
And imagine, for only $750, you too can listen as this convicted war criminal fills these scouts' heads with the glorious tale of how he, Oliver North, planned the successful invasion of the second-smallest independent country in the Western Hemisphere; the mighty island of nation of Grenada!<p>
In a sane world, North would be rotting behind bars and the Boy Scouts would bring Camilo Mej&iacute;a to speak on why he resisted the war in Iraq.<br>
<b>Ben Lassiter,</b> Greensboro, N.C.<p>

