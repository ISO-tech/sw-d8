<p><a name="Burge"></a><font face="Times New Roman, Times, serif" size="4"><b>Jon Burge's rotten legacy</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I AM an African American community activist here in Gary, Ind. The many challenges we are faced with here are enormous. As you probably already know, we have the dubious honor of being labeled the murder capital of the country. One of our primary problems is self-hatred. That is not why I am writing though.<p>
On Monday, February 20, the mayor of Gary, Scott L. King appointed retired Chicago police officer of 28 years, Jeffery Kumorek, chief of police. Gary has a population of 104,000. Eighty-three percent are African American. <p>
Now, we have a white mayor and a newly appointed white police chief who is possibly a friend of fired Chicago police detective Jon Burge, the accused torturer of many young Blacks at the Area 2 police station in Chicago.<p>
I recall many cases of Burge torturing young Blacks in an attempt to persuade them to admit to various crimes they didn't commit. As a matter of fact, in 1972, I was interrogated at Area 2 concerning a murder I knew nothing about. Eventually, I was charged with armed robbery and murder. In November of 1973, I was rightfully exonerated.<p>
The reason for this letter is to request any information regarding individuals who not only participated in the torture cases with Burge, but every official who had knowledge of what was going on. Any participation Chief Kumorek had with Burge during not only the violation of my civil rights, but the violations of civil and Constitutional rights of every African American during Burge's era, will eventually be implemented here. <p>
We are in a potential state of martial law. It is transparent what could conceivably happen here in Gary, especially if Chief Kumorek think it is going to be business as usual. Any kind of data will be well received and appreciated.<br>
<b>Dwight Taylor,</b> Gary, Ind.<p>

