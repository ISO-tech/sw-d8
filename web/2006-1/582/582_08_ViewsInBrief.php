<p><a name="Cheney"></a><font face="Times New Roman, Times, serif" size="4"><b>Double standard for Cheney</b></font><p>

<font face="Times New Roman, Times, serif" size="3"><i>SOCIALIST WORKER</i> was right to use Dick Cheney's hunting debacle to point out his most egregious crimes--supporting South Africa's brutal apartheid regime in the 1980s and masterminding the devastation of Iraqi people since 1991, among countless others ("War crimes and hunting misdemeanors," February 24). <p>
But it must also be said that the way Cheney's assault with a deadly weapon--a crime, last I checked--was dealt with by law enforcement was glaringly hypocritical. Cheney shot an old man in the face and was allowed a full day to get his story straight. In the end, the local sheriff's department announced that it would take no action whatsoever. <p>
This stands in stark contrast to the treatment of so many youth of color by the criminal injustice system. For us, the cops arrest first and ask questions later, and for much less serious crimes than shooting people. The hypocrisy and bankruptcy of the so-called upholders of justice, from the courts of international law, right down to local law enforcement agencies couldn't be clearer.<br>
<b>Khury Petersen-Smith,</b> Boston<p>

