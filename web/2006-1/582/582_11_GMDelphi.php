<font face="Times New Roman, Times, serif" size="4">Buyout at GM and Delphi will destroy UAW jobs</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"This is union-busting"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 31, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">WITH THE bribe of cash upfront for workers who leave and the threat of a slash-and-burn bankruptcy for those who stay, General Motors and Delphi are out to cut tens of thousands of jobs--and the United Auto Workers (UAW) leadership is backing their move.<p>
By offering buyouts to more than 100,000 North American employees, GM aims to eliminate 30,000 jobs by 2008.<p>
"This isn't a buyout, it's a buy off," said Gregg Shotwell, a Delphi worker at the Coopersville, Mich., plant and an organizer of the Soldiers of Solidarity (SOS), a dissident UAW group that has held a series of rank-and-file meetings across the Midwest since Delphi declared bankruptcy last fall.<p>
Shotwell pointed out that GM's offer of $35,000 for workers to retire early is the equivalent of just six or seven months wages for an assembly plant worker that works overtime.<p>
For workers like Shotwell who are within three years of the 30-year mark for full pension benefits, GM and Delphi would pay 60 percent of the pensions until they reached normal retirement date, plus health care benefits. "That would be a 40 percent drop in my income," he said. "Most workers eligible want to keep working, because this is the time in their lives when they would save the most money."<p>
Workers with 10 or more years of seniority will be offered $140,000 to retire and surrender all benefits other than accrued pension benefits. "This is not a union solution," Shotwell said. "It is union busting in the extreme. People are put in the position of making a choice as an individual, when the good of all would be to fight to keep the jobs."<p>
The other question is what benefits will be negotiated for retirees in future contracts, given that the UAW leadership last year pushed through a vote for major cuts in retiree health care. <p>
"For months and months, they have been telling us that GM and Delphi are in trouble because of their legacy costs"--primarily pension and health care costs for retirees, Shotwell said. "Now they want us to add to those legacy costs by retiring early, and tell us that it will solve their problems. They want us to jump from one sinking ship to another."<p>
There won't be a chance to vote on the issue in UAW meetings, however. The deal will be offered on an individual basis, with workers given 45 days to accept.<p>
At Delphi, GM's former parts subsidiary that is now in bankruptcy court, the stakes are even higher. CEO Steve Miller has threatened to ask a bankruptcy court judge to void the union's contracts if they don't agree to concessions. Of some 23,000 UAW members--70 percent of the company's workforce--5,000 workers are eligible to "flow back" to GM, if they're willing to move to find a job.<p>
Those who don't take the early retirement deal will see their wages cut from about $27 per hour to $22 immediately, then to $16.50 per hour in September 2007.<p>
To entice workers to accept the deal, Delphi will reportedly offer $50,000 payouts, though details are unclear. Health care for retirees would be eliminated, and the right to strike prohibited.<p>
UAW leaders have vowed to strike if Delphi voids the union's contracts, but have made no move to authorize a walkout. Todd Jordan, a member of SOS and a member of UAW Local 292 at Delphi's Kokomo, Ind., plant, doesn't expect UAW officials to organize resistance, so the SOS committee is building a network of activists within various shops and shifts in the plant.<p>
"I think these buy-offs are an absolute tragedy and betrayal of unionism," said Jordan, who has 11 family members who have worked for GM and Delphi for several decades. "This is a way [for the UAW] to mitigate resistance to concessions and lost jobs. I think we are selling out our future." <p>
As one of the younger workers in the Kokomo plant--he's had seven years on the job--Jordan plans on staying and fighting, wage cuts or not. "I'll be here," he said, "until the last ship sails."<p>

