<font face="Times New Roman, Times, serif" size="4">How Tulia's racist cops got caught in their lies</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The "war on drugs" in a Texas town</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Mike Corwin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 3, 2006 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Nate Blakeslee, <b><i>Tulia: Race, Cocaine, and Corruption in a Small Texas Town.</b></i> Public Affairs, 2005, 450 pages, $26</font><p>

<font face="Times New Roman, Times, serif" size="3">IN THE summer of 1999, 47 women and men, most of them African American, were arrested for drug trafficking in the small town of Tulia, Texas.<p>
Media were called in to document the arrestees being hauled in during the early morning hours, and the next day the local paper declared "Tulia's Streets Cleared of Garbage." The arrests were said to be the fruit of a months-long sting by Tom Coleman, an undercover narc working with a regional Narcotics Task Force.<p>
The arrests and subsequent convictions in most of the cases were based solely on the word of Coleman, who had no witnesses, notes or other corroborating evidence of the alleged crimes. Four years later, the cases unraveled under legal and public pressure, culminating in a dramatic 2003 court hearing that reversed all the convictions.<p>
Journalist Nate Blakeslee--who broke the Tulia story in the pages of the <i>Texas Observer</i>--revisits the dusty town in the Texas panhandle in <i>Tulia: Race, Cocaine, and Corruption in a Small Texas Town. </i>In <i>Tulia</i>, Blakeslee explains how this atrocity could take place--and depicts the struggle of the people who fought for justice.<p>
In Blakeslee's depiction of Tulia, you can feel the racism pervading the town. He writes, "...race was a central organizing principle of life in Tulia," affecting everything from jobs to dating to sports.<p>
Tulia had briefly experienced an agricultural boom, but in recent years hit on hard times. Increasingly, the town's tiny Black population found itself in a climate of scapegoating and police harassment, in which the 1999 bust seems like an inevitability.<p>
The tone is set in a scene from the book's first chapter, in which Coleman pays a visit to the arrestees on the day of the bust. Coleman tells the men assembled in jail, "You niggers quit selling them drugs!"<p>
As the defendants go to trial, they are astonished as they go up against a "justice system" more inclined to railroad them than explore the serious flaws in the cases. After the first round of convictions and draconian sentences--one defendant received 361 years--many took plea bargains despite their innocence.<p>
But over the years, the arrestees, their lawyers, activists and sympathizers in the community uncovers a conspiracy to cover up for Coleman, who, it turned out, had some legal problems of his own. As his sting was underway, Coleman was arrested on charges that he had stolen gasoline during his tenure as a cop in another Texas town.<p>
The effort by Coleman--and the local sheriff and district attorney--to hide this and other unsavory aspects of his past becomes the fissure from which the whole bust eventually comes apart.<p>
The last third of the book reads like a courtroom thriller, detailing the 2003 post-conviction hearing for four of the defendants in which a "dream team" of lawyers expose Coleman's deceptions. The focus on the legal proceedings makes for exciting reading, but perhaps takes away from time that could have been spent detailing the human impact of the bust.<p>
Only a few of the arrestees--Joe Moore, Donnie Smith and Freddie Brookins Jr.--have their personal stories told in detail. Those three--along with Jason Williams--were the applicants in the 2003 hearing and are the real heroes of the story.<p>
As the cases against them fall apart, they hold out for a legal settlement that would help all those convicted, even when doing so could have jeopardized their freedom. Blakeslee exposes the reality of the "war on drugs," revealing the lack of accountability in drug enforcement programs in which "whole taskforces of Tom Colemans" are at work.<p>
<i>Tulia</i> makes for compelling reading, not only for its exposure of police corruption but also for the inspiring story of those who fought back and won. Unfortunately, racist scapegoating such as the events depicted in this book takes place every day in our society, usually with less of a happy ending.<p>

