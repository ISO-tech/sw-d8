<font face="Times New Roman, Times, serif" size="4">Ten grad employees told they'll lose a year's pay</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>NYU threatens strikers</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sarah Wolf, GSOC</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 3, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--New York University (NYU) is cracking down in an attempt to break a strike by graduate employees. In the past week, 10 of the few hundred grad employees on strike since November 9 for recognition of their union received notices that they would be docked two full semesters' pay. <p>
These unprecedented penalties raised the stakes and show just how far the administration is willing to go to crush union organization on campus. The attack may also reinvigorate a strike that is currently weaker than it was in the fall.<p>
The week that the first threats were issued, students in several academic departments met to decide what we can do to defend the targeted strikers. Teachers in the Spanish department, for instance, will be notifying the administration that they may not turn in grades or perform other necessary work if their colleagues are punished. <p>
A January 26 rally of more than 1,000 union workers marched around Washington Square after hearing Transport Workers Union Local 100 President Roger Toussaint, United Federation of Teachers President Randi Weingarten and others express solidarity with Graduate Student Organizing Committee/United Auto Workers (GSOC/UAW) Local 2110's strike.<p>
But with the university threatening strikers with stiff punishment, it's time to turn talk of solidarity into more action and on-the-ground organization.<p>
Over the past few weeks, GSOC has urged members to remain on strike while pursuing a strategy based on the hope that politicians will force the university to negotiate. But this hasn't inspired confidence among GSOC members that the union has a concrete strategy to win.<p>
Rank-and-file activists have put energy into convincing their colleagues to remain on strike and building ties with Teamsters at UPS and DHL to stop deliveries to NYU. But so far, such efforts have not succeeded in turning around the union's wait-and-see strategy. <p>
Now we have an opportunity to up the ante--by waging a defense campaign that connects with activists and union militants outside our union, while throwing light on why it is so important that graduate employees have a union in the first place. Activists in the union and beyond will start taking next steps this week--from letter-writing campaigns to more visible protests. <p>
Forcing the university to drop the threatened penalties could give strikers the confidence to fight, but allowing the punishments to go forward could be even more disastrous than ending the strike without a victory.<p>
"We need to be responding to this immediately, and the union needs to be doing much more," said Sarah Townsend, one of the targeted strikers and an activist in the Spanish Department. "We have an opportunity to make a big deal out of it now. This so-called punishment is meant to intimidate anyone on campus who might consider taking a stand in opposition to the administration. If they know they can get away with it, they'll do it again."<p>
