<font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOUR ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The Democrats: Fulla-bluff-and-bluster</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 3, 2006 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">JOHN KERRY'S scheme for a last-ditch filibuster blocking Judge Samuel Alito's Senate confirmation vote on Monday backfired badly. <p>
To be sure, Kerry made an impassioned plea last Thursday. "It's our right and our responsibility to oppose [Alito] vigorously and to fight against this radical upending of the Supreme Court," his statement read. <p>
Yet Kerry was not even present to read the statement in person last Thursday, or to attend the full Senate hearings. He had jetted off to Davos, Switzerland, to join other representatives of the rich and famous at the World Economic Forum. His "vigorous" opposition was apparently conducted by phone (conjuring up images of Kerry calling out, "Save me a seat at the bar, Bono. I just have to make a phone call.") <p>
Kerry's eleventh-hour campaign might seem in contradiction to explicit instructions from the party's powerbrokers in the Democratic Leadership Council (DLC), issued last Tuesday. "A filibuster is certain to fail," the statement read. "The second-best way for Democrats to avoid still more Alitos on the Court is to make major gains in the Senate this November. And the best way is to win the White House in 2008." <p>
But Kerry's scheme was also launched with an eye toward 2008. His feeble maneuver came only when it was clear that Republicans already had the 60 votes to defeat an attempted filibuster. <p>
It reeked of a five-day publicity stunt intended to rack up Kerry's liberal credentials for the 2008 presidential race, so that he could abandon them once his campaign actually gets underway. This formulaic replay of his ill-fated 2004 strategy is entirely in keeping with the DLC's priorities. <p>
Once presidential hopeful Kerry supported an Alito filibuster, however, the party's wheels were set in motion. The rest of the election-bent pack was required to act in kind--not to be upstaged by Kerry, since their own futures also depend on the certainty of a liberal voting base, however much they wish to forget that uncomfortable fact. <p>
Hillary Rodham Clinton, previously silent on filibustering Alito, chose to attend a Seattle fundraiser instead of the Senate hearing on Friday. But from Seattle, she issued a statement supporting Kerry's initiative, "History will show that Judge Alito's nomination is the tipping point against constitutionally based freedoms and protections we cherish as individuals and as a nation."<p>
Thus, the tide began turning, as one Democrat after another reluctantly jumped on the filibuster bandwagon. <p>
Just two weeks earlier, California Sen. Dianne Feinstein announced she would not block Alito's nomination unless he was guilty of "moral turpitude." By Friday, she issued a statement that she, too, would vote to delay his confirmation.<p>
Senate Minority Leader Harry Reid made his opposition to a filibuster clear last week, repeatedly urging, "This matter will be resolved without too much more talking." But by Saturday, he had joined the growing list of influential Democrats, including Sens. Russell Feingold, Joseph Biden, and Barack Obama, along with Minority Whip Dick Durbin, vowing to vote to filibuster. <p>
Their lack of enthusiasm was nevertheless notable last weekend. Reid told reporters that "everyone knows" Senate Democrats could not garner the 40 votes needed to win. Obama told ABC News' <i>This Week</i>, "These last-minute efforts--using procedural maneuvers inside the Beltway--I think has been the wrong way of going about it." Biden commented on CNN's <i>Late Edition</i>, "I think a filibuster makes sense when you have a prospect of actually succeeding"--but predicting that this effort was doomed.<p>
If Democrats had sincerely intended to organize an opposition to Alito, they would not have waited until the eleventh hour to be dragged toward a last-minute filibuster attempt. <p>
Republican conservatives began mapping out their strategy last February, at the first mention of Chief Justice William Rehnquist's fatal illness. Even so, Spencer Abraham, a member of the conservative Federalist Society and Bush's former energy secretary, said Alito's confirmation would have been "beyond our best expectations."<p>
In the end, the strongest "opposition" Democrats could muster was to delay the vote to confirm Alito by 30 minutes at most. Their rallying cry, "Vote your conscience," only showed the world that they have none.<p>

