<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">February 3, 2006 | Pages 10 and 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Stop Chicago school closings</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jesse Sharkey, CTU Delegate</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--Chicago Public Schools (CPS) chief Arne Duncan announced January 25 that the district faced a $328 million budget deficit and threatened to lay off 1,000 teachers.<p>
The next day, he called for the closing of four schools----Morse, Frazier, Farren and Collins High School--due to "persistent low performance." A fifth school, Sherman, will be handed over to a private contractor. All five schools are predominantly Black and overwhelmingly poor.<p>
The moves brought angry reactions from teachers and parents. "CPS is claiming that all of the receiving schools are better-performing than the schools they are closing," wrote Parents United for Responsible Education. "The truth is that <i>only</i> in the case of Farren will all students have the opportunity to attend a significantly better-performing school." In other words, the CPS plan will do nothing to improve the quality of education that students receive.<p>
Collins High School, for example, has been both "re-engineered" and "re-organized" over the past seven years, but has been devastated by the free-falling economy on Chicago's West Side and the destruction of public housing nearby.<p>
What will the closing of schools like Collins actually accomplish? It will create new opportunities for Chicago's privately run public schools--a non-union "system-within-a-system" that boasts 73 charter schools. The school privatization initiative, called Renaissance 2010, has received funding to start 15 new schools next year--11 of which are charters. <p>
Meanwhile, Duncan told reporters that the budget deficit would require cutting teachers and increasing class sizes from 28 to 31 students. Duncan also announced intentions not to pay $70 million that the city owes to the pension fund of the Chicago Teachers Union.<p>
"The funding crisis is legitimate," said teacher's union president Marilyn Stewart. "But for us to look at the [union] members to fund it or to take the hit for this is absolutely outrageous, and it's shameful."<p>

<font face="Times New Roman, Times, serif" size="4"><b>Solidarity with Colombian workers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Aaron Amaral</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--More than 150 people attended a January 29 forum on labor and human rights in Colombia that launched an initiative to bring together U.S. and Colombian trade unionists, Latin American activists, anti-sweatshop organizers and socialists. <p>
The forum was organized through the offices of local councilman Hiram Montserrat and grew out of a report released by the New York City Fact-Finding Delegation on Coca-Cola in Colombia. The report, and much of the forum, focused on the complicity of Coca-Cola in human rights abuses arising from ongoing efforts by trade unionists to organize and protect Colombian Coca-Cola workers.<p>
The forum opened with statements of solidarity from U.S. trade unionists, including members of the Hotel Employees and Restaurant Employees, Teamsters and the Professional Staff Congress at City University of New York.<p>
The forum included three members of Sinaltrainal, the Colombian food and service workers union. Two of the unionists are currently in exile from Colombia--and are speaking to audiences in order to provide first-hand accounts of the assassinations, kidnappings, torture and ties between paramilitaries and Coca-Cola executives.<p>
A second panel featured indigenous rights and Afro-Colombian activists who spoke movingly of forced displacements caused by the strategic alliances between big business, large landowners, the Colombian government and paramilitaries. A third panel included academics and local immigrant-rights activists speaking about the role of U.S.-based activists and immigrants in raising awareness of Colombia's ongoing "dirty war" and the involvement of the U.S. government through Plan Colombia.<p>
Many spoke eloquently about the broader role of U.S. imperialism in the Colombian civil war and throughout Latin America. <p>
The forum was another step in bringing together local forces to help build a broad-based solidarity campaign that can have a real impact in stopping the Bush administration, their corporate sponsors and Colombian counterparts from pursuing their dirty war.<p>

