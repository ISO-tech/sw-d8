<font face="Times New Roman, Times, serif" size="5"><b>Activists rally to stop Vernon Evans' execution</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jon Van Camp and Rob Savidge</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 3, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">ANNAPOLIS, Md.--More than 60 demonstrators gathered in front of Maryland's State House January 28 to demand that Gov. Robert Ehrlich grant clemency to death row prisoner Vernon Evans, who is scheduled to be executed the week of February 6. <p>
Organizations present included the Baltimore Coalition Against the Death Penalty, the Campaign to End the Death Penalty, Maryland Committee Against State Executions (CASE), the Maryland Green Party, the Hip Hop Caucus, the World Can't Wait Campaign and the International Socialist Organization (ISO).<p>
Demonstrators marched to the front of the State House, while Vernon's family attempted to deliver petitions with hundreds of signatures to the governor's office. Dr. Evangelist Bates, Vernon's sister, also delivered a videotape for the governor to watch, which includes the testimony of the only eyewitness to the murder--attesting to Vernon Evans' innocence. ="Politicians--they don't care, when innocent people get the chair," chanted protesters.<p>
As Karen Jennings of the Maryland Green Party pointed out, Doug Duncan and Martin O'Malley, the Democratic candidates for the governor's race next year, have both refused to take a stand against the death penalty.<p>
But anti-death penalty activists are vowing to fight. "We know what we have to do to win, and we are doing it," said Mike Stark of the Campaign to End the Death Penalty.<p>
Jean Henderson of Maryland CASE said that the mood toward the death penalty in Maryland is changing. "The window is closing, so the Ehrlich administration thinks if it can't kill these prisoners this year, it may not get another chance," she said.<p>
ISO member Gene Alexeyev called out the hypocrisy of a state government that "will bring its whole weight to bear to carry out this execution, but won't lift a finger to stop the poverty and homelessness, which causes crime."<p>
Vernon Evans himself has joined in the fight for his freedom, speaking via phone at "Live from Death Row" events around the country. Recently, he attempted to participate in a Live from Death Row in Washington, D.C., but was prevented from calling in by the prison. Despite this, Vernon's friends and family helped to make the event a success--giving confidence to the more than 50 people who attended. <p>
Activists plan to hold another Live from Death Row in Baltimore on January 31 and will hold a demonstration February 4 at the Baltimore Supermax prison, where Vernon is being held.<p>

