<font face="Times New Roman, Times, serif" size="5"><b>Bush's plan for less health care</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 3, 2006 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THE BUSH administration decided their boss should talk about something that concerns millions of Americans, and they decided on access to health care. Thing is, Bush is talking about how to make health care less accessible.<p>
At the center of the Bush administration's scheme is a plan to promote Health Savings Accounts (HSAs). These high-deductible insurance policies cover only extreme medical expenses--participants would deposit money, tax-free, into special savings accounts to cover other medical bills.<p>
If you actually need health care, this plan would greatly increase your out-of-pocket spending. But for those who can already afford high health-care costs, the plan works out to be a big fat tax break.<p>
"Since wealthy people can afford insurance policies with high deductibles, and since HSA tax breaks are most beneficial for people in high tax brackets, the president's proposal will provide the most help for those who need it the least," says Ron Pollack, executive director of the health care watchdog group Families USA.<p>
According to the Bush administration, the accounts will help control health care costs by stopping patients from asking for "unnecessary health care"--because they would have to pay the bill. "You need to get people to have more skin in the game, as opposed to first-dollar insurance," Al Hubbard, director of Bush's National Economic Council, told Bloomberg News. <p>
But the health care system is a shambles not because people are getting "unnecessary" care, but because they aren't getting the care they need.<p>
Bush's HSAs would allow those with the most money to get out of company-provided health insurance, providing an excuse for corporations to further chip away at dwindling coverage for workers.<p>
The U.S. spends nearly twice as much on health care as other wealthy countries do, yet 46 million go without insurance. It's not because they haven't "saved" for it. It's because the health care and insurance industries are allowed to call the shots, no matter what the cost to patients.<p>

