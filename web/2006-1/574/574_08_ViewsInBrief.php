<p><a name="Kong"></a><font face="Times New Roman, Times, serif" size="4"><b>Kong's racist framework</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IN HIS review of <i>King Kong</i>, Joe Allen describes Joseph Conrad's <i>Heart of Darkness</i> (1902) as an "anti-colonial classic" ("<i>King Kong</i>: Is racism irrelevant?" January 6). Joe implies that the novel's cameo appearance is part of the "liberal veneer" that director Peter Jackson uses to mask a "deeply reactionary film." <p>
In fact, the reference to Conrad frames the racism of Jackson's film. Joe rightfully points to the ugly depiction of the natives of Skull Island as savages and beasts--but these images come right out of Conrad.<p>
Anti-colonial writers and critics have long rejected Conrad's novel. As African writer Chinua Achebe put it in 1977: "Conrad saw and condemned the evil of imperial exploitation but was strangely unaware of the racism on which it sharpened its iron tooth." <p>
In <i>Heart of Darkness</i>, Marlow, a man on quest up the Congo River in the 1890s, criticizes the brutality of the Belgian colonialism in the Congo. Kurtz, whom Marlow is seeking, has gone crazy in his jungle fortress, enslaving and murdering Africans. He symbolizes what happens to Europeans who pillage and plunder Africa.<p>
That's why the book was a bit more liberal than older European literature that simply praised colonialism as a "civilizing mission." After the great anti-colonial rebellions of the second half of the 19th century, European writers couldn't get away with rosy pictures of conquest. <p>
But the corruption of the "civilized" Kurtz is clearly shown to be a result of European contact with "savage" Africans--not the act of colonialism itself. Every page of the book bleeds with anti-Black racism. Marlow's greatest gesture of solidarity with Africans is when he wonders whether they are "not inhuman" after all. <p>
Indeed, liberal critics of U.S. foreign policy have long used Conrad to mask the racism underlying their own texts. Francis Ford Coppola's <i>Apocalypse Now</i> is a case in point (see Alan Maass' excellent review in <i>Socialist Worker</i>, "Apocalypse Then and Now," August 31, 2001). Coppola criticizes the war in Vietnam--even as he portrays the Vietnamese as Maass writes, as "savages who turn innocent American boys into monsters."<p>
That's why, for writers like Achebe who actually lived under the heel of colonialism, Conrad was nothing but a "thoroughgoing racist."<br>
<b>Pranav Jani,</b> Columbus, Ohio<p>

