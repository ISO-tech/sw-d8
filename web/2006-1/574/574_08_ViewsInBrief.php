<p><a name="Vons"></a><font face="Times New Roman, Times, serif" size="4"><b>How Vons busted a union</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THE VONS supermarket that I regularly shop at in North Park has recently undergone a complete remodeling: new floors, cashier stations, even a new Starbucks. Apparently, this remodeling is going on in many stores and must be enormously expensive. <p>
Normally, this wouldn't bother me; I have no issue with working-class neighborhoods having nice stores to shop in. The problem is that it was just three years ago that Vons was supposedly so hard-up that they had to lock out their Southern California employees in order to force them to accept concessions on their benefits and wages. They went to war with a bunch of old ladies and college students--forcing them to settle for a lousy contract that established a "two-tier" system.<p>
During the strike, a few comrades and I would visit the picket line at this same store to sell <i>Socialist Worker</i>, talk about the strike and other politics, and to find out how we could help. Exhausted and falling behind on their finances, they unanimously were ready to vote for this contract and get back to work--despite our arguments. Most of the cuts would only affect new hires, so some workers argued that they had actually come out ahead.<p>
Now when I shop at this store, I only recognize a few faces from the days of the strike. I have to assume that 80-90 percent of the people working there are on the lower tier: barely making a living wage and paying for most of their own benefits. <p>
Vons used the "two-tier" concession to gut the power of the union and to turn the store into a low-wage employer, and it happened very quickly. Maybe this is why they have the money now to remodel the store. I, for one, was happier when the workers there made enough to buy a home and raise a family.<br>
<b>Chuck Stemke</b>, San Diego<p>

