<font face="Times New Roman, Times, serif" size="4"><b>Support the dockworkers</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AS A result of their participation in the protests at the European Parliament meeting in Strasbourg, France, January 18, eight Belgian and one French dockworker have been sentenced to jail terms from one to four months. Another four dockworkers from Spain and France were given suspended sentences of up to five months.<p>
The convicted dockworkers were part of a protest action of about 10,000 dockworkers protesting planned schemes to cut back jobs on the docks and to undermine safety conditions at European ports. Ports across Europe were shut down as a result of protest strikes January 16-17. As a result of the protest, the European Parliament backed down from supporting this. <p>
The dockworkers were tried and convicted in a matter of a few days, due to a system of "fast-track justice" allowed in France. Their defense attorney had less than an hour to read the disposition of the case. This compares to an executive of a large corporation, in Belgium, charged with money laundering whose case has been waiting to go to trial for several years.<p>
The Belgian dockworkers, part of a contingent of almost 3,000, including 40 buses, were arrested by waiting police, upon their return to Antwerp. The Belgians appear to have been singled out for victimization. <p>
The workers' movement internationally must demand that all charges be dropped and the jailed dockworkers be immediately released. Workers' solidarity has no borders.<br>
<b>Ken Morgan,</b> Madison, Wis.<p>
