<font face="Times New Roman, Times, serif" size="5"><b>Fanning the flames of racism</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 3, 2006 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">ON JANUARY 14, a crowd of 200 antiracists from all over Massachusetts and Rhode Island thwarted an attempt of neo-Nazis to attack the Afro-American History Museum in Boston. While we defeated the Nazis, the racist climate in which they made their attempt remains.<p>
This tone of racism has been set by Mayor Thomas Menino, the Boston cops and the local mainstream media--all of which have whipped up a frenzy about crime in neighborhoods whose populations are largely people of color. Under the pretext of reducing last year's record homicide rate, recently re-elected Mayor Menino has made scapegoating Blacks the centerpiece of his new term. <p>
On December 1, Menino effectively outlawed a popular t-shirt that reads "Stop Snitchin'"--a reference to people who inform cops with details of crimes--by banning its sale in Boston. On December 3, Menino stormed a store that sells the shirts with an entourage of cops and conservative Black clergy to snatch the shirts off of the shelves. While the shirt may be obnoxious, it is a violation of free speech rights to outlaw its sale, and it's absurd to argue that a shirt contributes to crime. <p>
But that was just the beginning. In the following week, Menino announced the beginning of "Operation Red Zone"--a massive police crackdown on 10 crime "hot spots" in Roxbury, Dorchester and Mattapan--all of which are predominantly Black. The crackdown has involved Boston cops, state troopers, and federal law enforcement, including the FBI; Drug Enforcement Agency; Bureau of Alcohol, Tobacco and Firearms; U.S. Marshalls; and the Bureau of Immigration and Customs Enforcement. <p>
Recently, Menino announced a further crackdown, under "Operation Home Safe." Not only have well over 200 people been arrested in mass sweeps over the past month, but the city is also seizing the opportunity to deport untold numbers of immigrants.<p>
The recent police crackdown is an ineffective response to the growing crime in Boston. While Mayor Menino blames an unrelenting gang problem within predominantly Black and Latino neighborhoods, he neglects to mention the ever-worsening economic and social context. Menino's solution of putting more cops on the streets fails to address the real crime in the city: The fact that the unemployment and poverty rates are soaring. <p>
The largely Black and Latino neighborhoods of Roxbury and North Dorchester are the poorest in the city, with 32 percent of the population falling under the poverty level and unemployment hovering around 16 percent. Boston ranks 13th among the nation's poorest school districts with over 72 percent of Boston public school students qualifying for lunch assistance in 2002-2003.<p>
And, according to the Boston Foundation, Boston is the most expensive metropolitan region in the country to live in, over New York City and San Francisco.<br>
Democratic Mayor Menino and the majority-Democrat city council have taken the opportunity to cement a legacy of racism and an image of being "tough on crime." <p>
Antiracists and other community activists need to seize the opportunity to stand up to police repression and one party rule in Boston, and fight for a different future for this city.<br>
<b>Khury Petersen Smith and Tamar Szmuilowicz,</b> Boston<p>

