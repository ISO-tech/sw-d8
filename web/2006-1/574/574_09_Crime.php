<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Capitalism, crime and corruption</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 3, 2006 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">IN THE past few years, we have been treated to a parade of guilty rich people, caught with their hand in the till.<p>
The crimes are varied--insider trading, fake insurance scams, bribery masquerading as lobbying and CEOs cooking the books to hide loss and outright theft. It's refreshing to see some of these fat cats go down, a nice contrast to the usual script that plays out--police going after the "small fish."<p>
The word "corruption" is a misnomer, for it implies that there is something called honest capitalism. Crime and corruption is worked into the fabric of capitalism.<p>
Sir Francis Drake was a respectable fellow, right? Well, he made a fortune as a "privateer," the polite word for pirate. In addition to indulging in the very lucrative slave trade, he routinely captured and ripped off French ships. One nation's piracy was another nation's foreign policy.<p>
In the film <i>The Untouchables, </i>Al Capone tells a group of reporters, "You can get further with a kind word and a gun than you can with just a kind word." This is not only the creed of criminal gangs but also of states. What one state considers a criminal act--for example, the invasion of Iraq--another, the U.S., considers kindness with a gun.<p>
Corruption reaches its most frenzied peak at the height of economic booms, when everyone's trying to get rich. Speculation drives up stocks, and profits. Books are doctored to hide problems so that the good times can continue to roll. When the bubble bursts, the most egregious scandals are exposed.<p>
To retain an image of fairness, the courts prosecute a handful of cases. This does not eliminate corruption, but temporarily drives it underground. Faith is restored in the system, and the whole cycle begins again.<p>
But capitalists will engage in anything that is highly lucrative if they can get away with it. Moreover, if they are willing to line the pockets of politicians, they often <i>can</i> get away with it.<p>
"When I ride on the street cars," Clarence Darrow told a group of Chicago prisoners in 1902, "I am held up--I pay five cents for a ride that is worth two and a half cents, simply because a body of men have bribed the city council and the legislature, so that all the rest of us have to pay tribute to them." <p>
Author Ted Nace tells an instructive story in his book, <i>Gangs of America. </i>Beginning in the 1930s, a consortium of five automobile and oil companies conspired to buy up electric trolley lines and replace them will diesel-fuelled buses. By the 1950s, 90 percent of American cities no longer had electric trolleys. A federal jury convicted the companies of committing an antitrust violation. Each company was fined $5,000 and each executive officer a single dollar.<p>
"Nothing is illegal," remarked former Atlanta Mayor Andrew Young, "if 100 businessmen decide to do it." Laws have the appearance of being evenhanded, but because there is no economic evenhandedness, they aren't. Rich people don't need to shoplift for food. And they have far more effective means of picking pockets than pickpockets--they simply exploit other people's labor.<p>
A shoe fitter, a socialist who participated in the 1877 St. Louis general strike, put a fine point on this double standard: "A man who stole a single rail is called a thief, while he who stole a railway is a gentleman."<p>
"First and last, it's a question of money," Darrow said. "Those men who own the earth make the laws to protect what they have. They fix up a sort of fence or pen around what they have, and they fix the law so the fellow on the outside cannot get in. The laws are really organized for the protection of the men who rule the world. They were never organized or enforced to do justice. We have no system for doing justice, not the slightest in the world."<p>

