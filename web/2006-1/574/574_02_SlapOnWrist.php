<font face="Times New Roman, Times, serif" size="5"><b>Slap on the wrist for killing Iraqi detainee</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 3, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">CHIEF WARRANT Officer Lewis E. Welshofer committed murder. But he walked away from a military court-martial with nothing but a slap on the wrist last week--because his victim was an Iraqi.<p>
Welshofer, an Army interrogator, is the highest-ranking officer to be charged in connection with the abuse of prisoners in Iraq.<p>
In November 2003, Iraqi Major Gen. Abed Hamed Mowhoush was captured by U.S. forces in western Iraq. Already badly beaten, allegedly by CIA officials and contractors referred to as "civilian interrogators," Mowhoush was delivered to Welshofer for further interrogation. <p>
Welshofer tied Mowhoush with an electrical cord and stuffed him into a sleeping bag--and then sat on Mowhoush's chest until he suffocated to death.<p>
But according to his lawyers, Welshofer was simply following orders, and had been urged to "take the gloves off" while handling prisoners. The six-member court-martial jury agreed, convicting Welshofer not of murder, but on lesser charges of negligent homicide and dereliction of duty. <p>
Welshofer could still have faced a three-year prison sentence, but instead, the jury ordered that he receive a reprimand, give up $6,000 in pay and be confined to his base for 60 days. He won't even get a dishonorable discharge for his actions.<p>
While Welshofer gets off with a slap on the wrist, the military is moving to make sure some prisoners suffer the ultimate punishment. Last month, the Army quietly issued new regulations for carrying out death sentences imposed by general courts-martial or military tribunals.<p>
The biggest change would allow military executions to take place at sites other than Fort Leavenworth, Kan., where six men currently sit on the military's death row. Civil liberties experts fear that military tribunals could begin at the U.S. prison camp at Guant&aacute;namo Bay, Cuba--and that prisoners could be tried, sentenced and executed without ever getting a fair trial.<p>
The new regulations are also seen as a warning to disgruntled soldiers who might take out their anger on superior officers--like Sgt. Hasan Akbar, who was convicted and sentenced to death for killing two officers in Kuwait in 2003 on the eve of the invasion of Iraq.<p>
The only African American and Muslim in his unit, Akbar reportedly suffered harassment and abuse. And although a psychiatrist testified that Akbar suffered from forms of paranoia and schizophrenia, the military discounted this evidence, with the chief prosecutor calling him "a hate-filled, ideologically driven murderer."<p>
As the case of Chief Warrant Officer Welshofer shows, the military doesn't mind murder--as long as the murderer works for them.<p>

