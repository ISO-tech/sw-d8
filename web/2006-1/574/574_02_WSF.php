<font face="Times New Roman, Times, serif" size="4">Report from the 2006 World Social Forum in Venezuela</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The challenge to neoliberalism</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 3, 2006 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>LANCE SELFA reports from Caracas about this year's World Social Forum.</b><p>

<font face="Times New Roman, Times, serif" size="3">MORE THAN 80,000 people from 150 countries arrived at the World Social Forum (WSF) in Caracas, Venezuela, last week to take part in thousands of meetings and other events dedicated to the fight for social justice and the fight against war and imperialism.<p>
The meeting took on a special significance because it was held on the continent where the social movements of workers, peasants and the poor have conducted the most sustained counteroffensive against the free market dogma known as "neoliberalism."<p>
In Venezuela, the government of President Hugo Ch&aacute;vez has embarked on a number of experiments to harness the country's oil wealth to improve living standards, education and health care for ordinary Venezuelans, 60 percent of whom live in poverty.<p>
"Foristas," as Forum attendees were called, had only to look around the main venues to get a sense of what this means. Signs on the streets and in the subway stations touted the government's "missions," community-based social welfare, education and health programs. Government assistance was essential to facilitate the three-hour trek from the airport to Caracas, caused by the collapse of a major highway overpass.<p>
Ch&aacute;vez addressed the WSF twice during the week. At a Friday night rally at the Poliendro stadium, Ch&aacute;vez paid homage to Latin American anti-imperialists, from Cuba's Jose Mart&iacute; to Mexico's Pancho Villa.<p>
He contrasted George Bush, whom he lampoons as "Mister Danger" (a bloodthirsty rancher character in a classic Venezuelan novel), with "Mrs. Hope" who set up her "ranchito" outside Mister Danger's ranch. It was a tribute to Cindy Sheehan, who appeared on the stage with Ch&aacute;vez. "This century, we will bury the American empire, you can be sure of that," Ch&aacute;vez said.<p>
The WSF took place days after the inauguration of Evo Morales, a former coca farmers' leader and candidate of the Movement Towards Socialism (MAS) party, as Bolivia's president. More than any other left-leaning government on the continent, Morales owes his election to the power of working-class and social movement agitation that deposed two neoliberal presidents in the last three years.<p>
Oscar Olivera, a key leader of the Cochabamba "water wars" in which a mass movement forced the government and Bechtel Corp. to retreat from a plan to privatize water, explicitly linked the movement and the Morales victory during a Wednesday panel on the struggle for water. "It was the water, privatized in 1999, that made it possible for the people to speak out, to mobilize and to win," he said.<p>
The more than 2,000 "self-organized" events gave voice to a wide array of issues and concerns.<p>
At one afternoon session, foristas from three continents listened to a recorded message from Stan Tookie Williams, executed by the state of California in December, in a Campaign to End the Death Penalty meeting featuring Barbara Becnel, Williams' collaborator and spokeperson, and Campaign national organizer Alice Kim.<p>
At the same time, an audience spilling out into the hallways listened intently to a presentation by Venezuelan advocates of abortion rights. A bill to legalize abortion is expected to be presented in the Venezuelan legislature this year.<p>
The sheer number and variety of self-organized events, and the geographic spread of venues across Caracas, a city of 4 million, made it difficult to get a sense of the entire WSF and the key debates that animated most foristas.<p>
Clearly, political questions were on the minds of many foristas, and books, pamphlets and newspapers featuring such figures as Ch&eacute; Guevara, Lenin, Trotsky and Marx were available at just about every major venue. When Ch&aacute;vez endorses "21st century socialism," many activists are trying to figure out what that means for their countries and situations.<p>
As the Party of Revolution and Socialism, the newly formed Venezuelan party that held a number of events during the week, put it in a statement issued on the Saturday of the Forum: "Millions worldwide are looking for new leaders and new organizations that can lead struggles to victory... The Caracas Forum must help activists to learn about these new experiences and to take steps to bring together revolutionary sectors that propose a serious strategy for the fight against imperialism and capitalism and for socialism."<p>
<i>Brenda Couglin, Sarah Hines and Tom Lewis contributed to this report</i>.<p>

