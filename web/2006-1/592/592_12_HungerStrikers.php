<font face="Times New Roman, Times, serif" size="4">Hunger strikers protest immigration raids</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"We are not criminals"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Helen Redmond</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 9, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">TWO CHICAGO women facing deportation went on a nearly-month-long hunger strike to draw attention to their cause and the struggle to change immigration laws, which they say hurt and divide families. <p>
Elvira Arellano is a single mother who has lived in the U.S. for nine years and used to work at O'Hare International Airport, cleaning airplanes. On December 10, 2002 in a Department of Homeland Security operation dubbed "Operation Chicago Skies," Elvira and her young son were woken by Federal agents, and she was arrested and charged with possession of false documents--for using a fake Social Security number to get work, the Feds say.<p>
Flor Cris&oacute;stomo has lived in the U.S. for six years and worked at IFCO Systems, a company that manufactures wooden pallets. She was arrested along with 25 others in a workplace raid in April. Flor has three children in Mexico. <p>
As punishment for the mega-marches for immigrant rights, federal authorities have stepped up their workplace raids, including a major raid in April that rounded up more than 1,000 people, including the IFCO workers at a suburban Chicago plant.<p>
Flor and Elvira launched their hunger strike on May 10--Mother's Day in Mexico. <p>
"We have marched, tried a boycott, we have talked to the politicians in Washington," Flor said. "We can't use bombs against Bush. We can't use arms because we are not murderers. This is a very specific form of protest to make the government listen." <p>
Elvira echoes these sentiments. "I have been protesting these immigration laws for three years," she said. "We took two buses to Washington and met with congressmen, and I have protested in front of the Federal Building [in Chicago]. They are not listening." <p>
The hunger strikers camped out in a public plaza in Pilsen, a predominately Mexican American neighborhood. All day and late into the evening, the plaza was filled with supporters who brought water and Gatorade. There were dancers and drummers performing, along with speakouts, rallies and poetry readings in the plaza. <p>
The women have three demands: legalization with full rights for all immigrants in the U.S., an immediate moratorium on raids and deportations, and family reunification. As Evira points out, these demands are in direct opposition to the Hagel-Martinez "compromise" bill passed by the Senate last month and supported by some moderate immigrant rights organizations as "a step in the right direction."<p>
"I am in favor of legalization, not dividing people into categories," Elvira said. "We are human beings. Immigrants who have been here for less than two years would have to leave the country and try to get back in, and there are no guarantees they would get back in. This divides families of mixed immigration status." <p>
Elvira, who is the president of La Familia Latina Unida, has a message for the president of the United States. "Immigrants are not the problem," she says. "We work, we pay taxes, contribute to the economy. We want Bush to introduce a moratorium and stop the deportations. These laws separate families." <p>
On June 1, supporters organized a rally and picket in front of the immigration office in Chicago during the deportation hearing for Flor Cris&oacute;stomo and the other workers detained at IFCO. <p>
As Jose L&oacute;pez of the Puerto Rican Cultural Center told the crowed, "If the government believes in a free flow of trade, then it should encourage a free flow of workers across the border seeking work and a better life." <p>
During the rally, the IFCO workers came out of the building and explained that the deportations had been postponed for between two and four months, pending further hearings. After the rally, the two women ended their hunger strike, saying they had accomplished their goal of bringing more attention to the issue.<p>
At the rally, Elvira addressed the issue of whether undocumented workers in the U.S. were "illegal."<p>
"I am not a criminal or a terrorist," she said. "I don't steal. My only crime was to work to support my son. My son is an American citizen. In Mexico, the economy is not good. I didn't have work, and I didn't have money. How am I going to explain to my son that there is nothing to eat? I am looking for an opportunity to stay in this country to support my son, for his future."<p>
<i>Mark Dickman contributed to this story. For more information on the struggle of the IFCO workers, contact Roberto L&oacute;pez at 773-671-1727</i>. <p>

