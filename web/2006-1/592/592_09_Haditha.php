<font face="Times New Roman, Times, serif" size="5"><b>Haditha: The rule or the exception?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 9, 2006 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">AS I go through a list of <i>New York Times</i> articles on the Haditha massacre, in which Marines murdered 24 Iraqi civilians, the following phrases keep popping up: "unprovoked attack," "unprovoked murder," "unprovoked killings."<p>
This is presumably to counterpose the Marine's actions at Haditha to the more reasonable attacks in Iraq by U.S. armed forces--the attacks that are "provoked" by the behavior of Iraqis toward them. But can we make such a clean distinction?<p>
Defenders of the Iraq occupation will say "no" for their own reasons. They'll say--these guys are under a lot of stress fighting a shadowy insurgency, that the "enemy" can be anywhere, and when soldiers see their buddies killed right beside them, it's only natural that they lash out. To quote the <i>Chicago Tribune's </i>Mike Dorning, U.S. soldiers face "a foreign population in which friend, foe and bystander may seem indistinguishable."<p>
I'll try to give my own answer to this question by way of analogy. If someone conducts an armed invasion into your house and shoots you in your sleep, it is clear, is it not, that it is an unprovoked attack.<p>
Now, let's say you violently resist the armed home invader and, in the ensuing melee, he kills you--did you provoke him to attack? Is a condition of the home invader's attack being "unprovoked" that the homeowner submit peacefully to the home invasion? No, it would still be an unprovoked attack. The action of the homeowner, on the other hand, would be a provoked attack, i.e., an attack provoked by the home invader.<p>
The problem with the term "unprovoked attack" is that it deliberately obscures the larger picture--that the entire U.S. invasion and occupation of Iraq itself is an unprovoked attack by a foreign army on the Iraqi people. All U.S. armed action in Iraq is provocative in this sense; all Iraqi reactions to it are provoked by the occupation.<p>
It is shameful that we should even have to explain this, so numbed are many people in this country to the idea that the U.S. is perfectly within its rights to operate hundreds of military bases around the world and invade any country at will, and that it isn't legitimate for anyone to resist it.<p>
The top military brass lies when it says that massacres like Haditha are the exception. The entire U.S. military machine is a machine for violently enforcing U.S. will abroad.<p>
Soldiers (even more than U.S. civilians if that's possible) are trained in methods that dehumanize Arabs and Muslims to make it easier to kill them. Massacres of Iraqis--civilians and resistance fighters alike--are built into the situation.<p>
Military occupations almost always become total wars on the occupied population, because the majority seethe with hatred against the occupiers and eventually resist by whatever means at their disposal. Within that group, a significant minority are provoked to such outrage that they decide to take up the gun against the occupier.<p>
This is why the U.S. soldier cannot distinguish between friend and foe--occupying armies have very few friends--or collaborators, in the common vernacular. As the Haditha scandal develops, expect to hear stories about many more Hadithas.<p>
Of course, everything would go a lot more smoothly if the Iraqis would just accept heavily armed U.S. troops tramping through their streets, wrecking their infrastructure and shooting their family members. If Iraqis could just stop "provoking" American soldiers, then perhaps these damnable massacres wouldn't occur anymore.<p>
Alternatively, the resistance could identify itself a little better to American troops--perhaps wear green uniforms or wave little flags. During the American revolution one British officer complained: "Never had the British army so ungenerous an enemy to oppose; they send their riflemen five or six at a time who conceal themselves behind trees, etc., till an opportunity presents itself of taking a shot at our advance sentries, which done they immediately retreat. What an unfair method of carrying on a war!"<p>

