<font face="Times New Roman, Times, serif" size="4">Boston health center fires three immigrant workers</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Defend the right to unionize</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alpana Mehta</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 9, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">BOSTON--Two city councilors and a number of community organizations organized a picket and June 5 press conference to defend three workers fired May 24 for trying to organize an SEIU/1199 union at Martha Eliot Health Center, a subsidiary of Children's Hospital. The workers were fired without justification and without due process. <p>
Children's Hospital claims that there was a need for "new leadership" at Martha Eliot, but all the workers, who have worked at the health center between 12 and 19 years, have been central in winning medical services for the Latino community, including the only Spanish-language psychiatric care service available in Massachusetts. <p>
The three women--Ana Lamarche, Nicolasa L&oacute;pez and Ana Ortiz--are also leading organizers within the immigrant rights movement, helping to organize May 1 actions in both New York and Boston.<p>
Not only did the firing of these HIV and mental health care workers infringe on their labor rights, but it also represented a threat to the future existence of the few health-care services oriented to immigrants from Cape Verde, Haiti and Latin America. More than 80 percent of employees at Martha Eliot are Latino, as are more than 90 percent of the patients. <p>
Among the 40 people at the picket was a client of one of the fired workers. "This is about humiliating the Latino community," said the patient. "They didn't even give them a reason why they were fired. They're trying to divide the workers against each other."<p>
A recent community meeting to defend the three women included Martha Eliot workers, patients and community members directly affected by the firings. Patients expressed their outrage, and some refused to go back to Martha Eliot out of solidarity with the fired workers, but are faced with the fact that other local HIV services do not provide enough Spanish-translation.<p>
The Massachusetts Senate has been following the lead of the U.S. Senate in its drive to pass draconian anti-immigrant laws. This attack shows that bosses at Children's Hospital are using those attempts to initiate a broader attack on immigrants' lives. This is a fight for immigrant rights as much as it is for the right to unionize.<p>
<i>A picket has been called in front of Martha Eliot Health Center on June 7 from 4 to 6 p.m. to defend the fired workers, and there will be rally at Children's Hospital on June 19 at 11 a.m. For more details and information about future actions, call 857-389-2120. Julie Keefe and Rebecca Bor contributed to this report</i>.<p>

