<font face="Times New Roman, Times, serif" size="4">Minutemen plan to build their own fence</font> <br>
<font face="Times New Roman, Times, serif" size="5"><b>The vigilantes' wall of racism</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 9, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">CLAIMING THEY are doing "the job our government refuses to do," members of the anti-immigrant Minuteman Civil Defense Corps began construction of their own border fence in southeastern Arizona on Memorial Day weekend.<p>
The vigilante group chose this 10-mile stretch of land because it is the site where they began "patrolling" the border in November 2002, said Chris Simcox, founder of the group.<p>
The initiative symbolizes the growing prominence of the Minutemen, whose racist hate of immigrants, once dismissed as the crackpot hysteria of the far right, has become accepted in mainstream political debate.<p>
The racists are building the fence--a combination of barbed wire, razor wire and steel rail barriers--on the private land of local ranchers. "We're not going to stop," said Timothy Schwartz, a contractor who traveled from Glendale, Calif., to attend the "groundbreaking." "We're going to stay here with a group and keep building." Minuteman spokesperson Connie Hair says that the group has raised some $380,000 for more fences.<p>
The group says it is responding to the government's inability to "protect" the border, but their demands have been bolstered during the congressional debate over legislation that further restricts immigration.<p>
George Bush has already vowed to deploy some 6,000 National Guard troops to beef up the U.S. Border Patrol. And the Hagel-Martinez immigration bill that recently passed through the Senate--trumpeted by Democrats as a pro-immigrant "compromise"--promises 370 miles of new high-security border fences. This is giving legitimacy to the idea of further militarizing the border and the vigilante patrols of the Minutemen.<p>
 Since a major increase in border enforcement with Bill Clinton's Operation Gatekeeper in 1994, immigrants have been forced to cross in increasingly dangerous areas. This includes the desolate terrain of the Sonora desert on the border of Arizona, where temperatures regularly reach 100 degrees and more.<p>
Last year, the Border Patrol reported finding 463 dead bodies of immigrants in the southwestern United States, a 42.5 percent increase over 2004. "When we find them, their skin is like cardboard," Berta De la Rosa, a Mexican official who works on reducing the number of migrant deaths, told Reuters. "Often they have torn off their clothes or buried themselves in the sand to escape the heat."<p>
In anticipation of an even deadlier summer this year, Bruce Parks, head of the Tucson, Ariz., medical examiner's office, said that his morgue would buy a refrigerated trailer to store bodies. "If they shut down some of these other areas, people will likely keep coming across in places where there aren't fences," Parks told Reuters.<p>
If new congressional legislation passes, more immigrants will die as a result. Anyone who cares about immigrant rights must oppose more border fences--whether they're built by Minutemen or the U.S. government.<p>
Last weekend, antiracist protesters confronted Minutemen in several cities where they tried to rally, including at the Mexican consulate in New York City. Confronting these bigots when they try to organize is the best way to make sure they don't grow stronger and more confident.<p>

