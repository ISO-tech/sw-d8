<font face="Times New Roman, Times, serif" size="5"><b>Democrat to sign Louisiana abortion ban</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 9, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">LOUISIANA GOV. Kathleen Blanco--the first woman governor of the state and a Democrat--announced June 1 that she will sign new legislation that bans almost all abortions.<p>
Under the proposal, no woman would be able to get an abortion in Louisiana unless it can be proved that she might die or her health would be permanently damaged.<p>
According to the bill--which passed the state House May 30 in an 85-17 vote--a doctor who performs abortion procedures could be fined $100,000 and imprisoned up to 10 years. A proposed amendment that would make exceptions in cases of rape and incest was rejected.<p>
Now the legislation returns to the Senate, which passed a similar ban that allows abortion only when a woman's life is in danger. "The legislature has spoken," Blanco said.<p>
Earlier this year, the South Dakota state legislature passed an all-out abortion ban aimed at overturning <i>Roe v. Wade</i>, the U.S. Supreme Court's decision that made abortion legal in 1973. Pro-choice activists have gathered some 38,000 signatures on petitions to get a referendum on the November ballot to put the ban to a statewide vote.<p>
Here again, the Democrats failed to measure up to their pro-choice image. Both Democratic candidates for South Dakota's governor's race have stayed away from the issue of abortion. "I think we spend very little time on it in the discussions," Democratic candidate Dennis Wiese told the Associated Press. "Basically, there are so many other issues facing the people of our state."<p>
If abortion rights supporters are going to keep women's health out of the back alleys, we have to take these bans head on. <p>


