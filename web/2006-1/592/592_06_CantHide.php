<font face="Times New Roman, Times, serif" size="4">Full horror of the U.S. war on Iraq revealed</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The war crimes they can't hide</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 9, 2006 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">THE FULL horror of the U.S. Marines' massacre of Iraqi civilians at Haditha has exposed the true face of the U.S. occupation of Iraq for the world to see.<p>
But reports of what the military's own investigation of Haditha revealed have been accompanied by emerging stories about other massacres--showing that Haditha is just one of many horrors inflicted on Iraq by the U.S. occupiers.<p>
For ordinary Iraqis, Haditha marks a rare occasion--when the crimes of the U.S. "liberators" may actually see the light of day. What happened at Haditha--where U.S. Marines rampaged through several homes on a day last November, eventually killing 24 unarmed civilians, including several children and elderly men and women--is only now becoming known.<p>
But Iraqis have been subjected to Haditha-style horrors ever since the U.S. war and occupation began.<p>
Just ask the families of those killed in March in the town of Ishaqi, north of Baghdad. While the U.S. military claimed that four people were killed when a house collapsed following a shootout with an "al-Qaeda operative," witnesses say a total of 11 civilians--including as many as five children under the age of 5, and a 75-year-old woman--had been gathered together in the corner of one room, with their hands bound, and were then shot to death by U.S. troops.<p>
Iraqi police reports, as well photos from Agence France Presse and recent video obtained by BBC News, contradict the U.S. account of the killings. The video shows a number of dead adults and children at the site with what BBC world affairs editor John Simpson and others say are clearly gunshot wounds.<p>
Yet reeling from the charge that U.S. troops committed war crimes in Haditha, the U.S. military has already closed its "investigation" into Ishaqi, dismissing allegations of wrongdoing. The dead civilians? "Collateral deaths," according to the Pentagon.<p>
Just how many civilians have lost their lives or been brutalized at the hands of U.S. troops is impossible to know, since the Pentagon refuses to keep track of Iraqi casualties. But in May alone, according to the Associated Press, 936 Iraqis were killed in war-related violence--and 82 percent those deaths were civilians.<p>
By now, the pattern is all-too-familiar: U.S. forces attack, civilians are killed, and the U.S. claims that those who lost their lives were either "terrorists" engaged in an attack, unavoidable "collateral damage," or the result of an "accident" that U.S. troops can't be held responsible for.<p>
That includes the deaths of 60-year-old Khairiya Nisiyif Jassim, 20-year-old Anaam Zedan Khalafaged, and 40-year-old, mentally handicapped Khaled Zedan Khalaf--all killed in early May in an attack by the 101st Airborne Division in Samarra,<p>
Zedan Khalaf Habib, the husband of Khairiya and father of Anaam and Khaled, told Reuters last month that U.S. troops indiscriminately fired into a room where 15 frightened people had gathered to escape a nearby gunfight. Later, said Zedan's daughter Shireen, U.S. troops staged the evidence to make it look as though her mentally handicapped brother had been an "insurgent."<p>
"After they killed my brother Khaled, they shot him three more times in the chest, they put a rifle between his legs to show he was armed, and they took a photograph of him," she said.<p>
Military officials insist, however, that the people they killed had "planned to attack the soldiers."<p>
Meanwhile, the Pentagon is explaining the death of Nabiha Nisaif Jassim and her cousin, Saliha Mohammed Hassan as a "regrettable," but unavoidable, loss of life. Late last month, Nabiha, pregnant and in labor, was being driven to the hospital in Samarra by her brother, Khalid, with their cousin Saliha also in the car. <p>
But they never made it. U.S. troops opened fire on the car after it supposedly "entered a clearly marked prohibited area near coalition troops." The two women were killed. "I was driving my car at full speed because I did not see any sign or warning from the Americans," Khalid told BBC News.<p>
Khalid spoke for many Iraqis, when he added, "God take revenge on the Americans and those who brought them here. They have no regard for our lives."<p>


