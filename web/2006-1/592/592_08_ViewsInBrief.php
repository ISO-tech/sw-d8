<font face="Times New Roman, Times, serif" size="4"><b>New Orleans' mercenaries</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I READ your article on the ZNet Web site, and I was glad to finally get a few answers to questions that I've asked since last year ("A mercenary army," June 2). <p>
My name is Michael Dardar, and my family and I lived in southern Plaquemines Parish, a few miles south of where the eye of Katrina made landfall. Needless to say, we lost our home and most of our possessions to the storm. <p>
One of the things that got my attention early on was the presence of at least four Blackwater agents at every FEMA relief center I visited to process my application for aid. I relocated to Raceland, La., and there was a FEMA office just down the road from my house until about the end of February or March. And yes, there were four armed mercenaries there the whole time.<p>
Why was that level of security needed in rural Louisiana, and how much of the funds dedicated to hurricane relief went into the pockets of these politically connected "security firms"?<br>
<b>T. Mayheart Dardar,</b> from the Internet<p>

