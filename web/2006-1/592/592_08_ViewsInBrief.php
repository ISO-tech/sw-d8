<p><a name="Hijab"></a><font face="Times New Roman, Times, serif" size="4"><b>Hypocrisy over the hijab</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IT WAS not long ago that France passed its ban on the wearing of religious symbols in public schools, declaring that the headscarf worn by some Muslim women threatens secular society. Many on the French left, including the leaders of key militant workers' organizations, agreed that Muslim girls needed governmental protection from religious repression. <p>
Yet if French officials hold secularism so dear, why were all government offices, countrywide, closed on May 25 in observance of the Catholic holiday of Ascension? If the government is truly concerned about women's rights, why hasn't it challenged the Church's attacks on birth control and abortion rights as a clear threat to women's autonomy? <p>
Clearly, the ban on the hijab has nothing to do with safeguarding secularism and championing women. Instead, by promoting anti-Arab racism and national chauvinism, the government has tried to protect itself from a powerful potential challenge to its security-shredding agenda: a united French working class.<br>
<b>Nancy Welch and Didier Delmas,</b> Toulouse, France<p>

