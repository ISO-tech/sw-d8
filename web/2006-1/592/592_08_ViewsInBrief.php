<p><a name="Etim"></a><font face="Times New Roman, Times, serif" size="4"><b>Australian left and East Timor</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IT'S RATHER galling here in Australia to read in <i>SW</i> quotes from <i>Green Left Weekly</i> about the lack of good intentions by the Australian government in East Timor ("Behind the violence in East Timor," June 2). <p>
<i>Green Left Weekly</i> supported--indeed, campaigned for--the Australian invasion in 1999 and are not now opposing this colonialist takeover. They are only calling for the government to take notice of the needs of the population!<p>
Any self-respecting socialist in Australia should be openly and clearly against all military intervention by Australia. Australia is a major military power in the immediate region. It is one of the most strident supporters of U.S. imperialism. <p>
It was absolutely predictable that the Australian ruling class would not develop East Timor after 1999. Both the conservative Liberals and the Labor Party have supported the Indonesian occupation since 1975. <p>
Their purpose in 1999 was to end "instability," the constant refrain used against every national liberation or democratic movement in this region--which they call "our backyard"--and to get control of oil in the Timor Sea. The fact that most of the Australian left not only supported, but called for, Australian troops to intervene in 1999 gave credibility to the military of an imperialist power and legitimized the biggest defense budgets in years. <p>
It is not credible for those who called for Australian imperialist intervention to now cry crocodile tears about the government's indifference to the rights and wellbeing of the mass of East Timorese.<br>
<b>Sandra Bloodworth</b>, Socialist Alternative, Australia<p>

