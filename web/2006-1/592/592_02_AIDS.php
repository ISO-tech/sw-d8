<font face="Times New Roman, Times, serif" size="4">Chief obstacle at UN meeting marking 25th anniversary</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>U.S. blocks AIDS prevention effort</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 9, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">TWENTY-FIVE years after the official acknowledgement of the AIDS epidemic, the U.S. government is continuing its long record of blocking prevention measures.<p>
According to news reports, the Bush administration joined with several Muslim countries and conservative nations in Latin America to block important provisions in a renewed United Nations (UN) effort to fight AIDS. The U.S. objection? That distribution of condoms and needle exchange programs should be matched by promotion of sexual abstinence.<p>
The UN-sponsored international meeting last week was organized to update a 2001 declaration that committed to halting the AIDS epidemic. But only a few countries met their targets from five years ago. <p>
And in negotiations over the conference's final declaration, U.S. officials insisted that "any mention of condoms has to be matched by abstinence and faithfulness," Sisonke Msimang, a spokesperson for the Open Society Initiative for South Africa, told Britain's <i>Guardian </i>newspaper.<p>
As a result of U.S. pressure, the declaration failed to mention people most at risk for contracting the virus--including prostitutes, gay men and intravenous drug users.<p>
U.S. officials also succeeded in stopping the conference from setting exact financial targets for funds to be devoted to the effort to stop AIDS. Why? According to the <i>Washington Post, </i>administration officials fear that if there were shortfalls in meeting the targets, the U.S. would be blamed.<p>
U.S. media reports on the conference tended to emphasize the advances in stopping the spread of AIDS. The disease has killed 25 million people around the world. Nearly 40 million are infected today--and the vast majority has not been tested and is unaware of their status.<p>
The infection rate is slowing down globally, but in certain regions and countries, it is still on the rise.<p>
In particular, Africa remains an epicenter of the disease--and three-quarters of those infected with the HIV virus are women. "I think in Africa, it is only comparable in demographic terms to the slave trade regarding the impact it has had on the population," said Peter Piot, head of the UN's AIDS agency. "In southern Africa, HIV prevalence continues to go up, and they're already the world record."<p>
Recently developed drugs and treatments are highly effective at prolonging the lives of HIV sufferers, but less than half of those infected around the world have access to medicine.<p>
The shortfall in meeting funding targets is stark. UN officials say that $15 billion will be needed to combat AIDS in developing countries this year, but only $9 billion is available. And the amount needed is expected to grow by nearly 50 percent in two years.<p>
Piot said it was time that world governments start developing strategies for dealing with the way that the epidemic's toll will affect future generations. "I think we will see a further globalization of the epidemic spreading to every single corner of the planet," said Piot. "There's an increasing diversity in how the epidemic looks."<p>

