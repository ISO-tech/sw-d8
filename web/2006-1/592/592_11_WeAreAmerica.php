<font face="Times New Roman, Times, serif" size="5"><b>"We Are America" debates in Chicago</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sam Jordan</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 9, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">ABOUT 150 representatives from across the U.S. attended a national meeting of the "We Are America" Alliance June 4 in Chicago with the aim of creating an "action plan" for the immigrant rights movement--but left divided on whether to support the immigration reform legislation that recently passed the U.S. Senate.<p>
The majority of people in the room voiced their concerns about the legislation, forcing the conference organizers to split the group into two committees. Those opposed to the legislation formed a "kill the bill" committee, and those who support it organized a "make the bill better" committee.<p>
From these two committees, representatives were elected to come up with a common message for the Alliance.<p>
The Alliance did agree to launch "Democracy Summer," a voter registration drive aimed at signing up a million immigrants by the November 2006 mid-term elections, and to support regional demonstrations in Chicago, Washington, D.C., and Los Angeles on Labor Day (September 4, 2006).<p>
Organizations attending included the Coalition for Humane Immigrant Rights of Los Angeles, the Illinois Coalition for Immigrant and Refugee Rights, the Massachusetts Immigrant and Refugee Advocacy Coalition, the National Association for Latin and Caribbean Communities (NALACC), the National Day Laborers Organizing Network, New York Immigration Coalition, ACORN, Jobs With Justice, the March 10th Movement (Chicago), the Chicagoland Student Network for Immigrant Rights, as well as labor unions, UNITE HERE, Service Employees International Union, the United Farm Workers, and the Laborers International Union of North America.<p>
The conference organizers said that the aim of the meeting was to continue to build the immigrant rights movement and to coordinate local and national organizations. They argued that the We Are America Alliance was simply a network and therefore could not take a position on current legislation approved by the Senate--a position which provoked a sharp debate on the floor of the conference.<p>
Frank Sharry, head of the National Immigrant Forum, which backs the Senate bill, argued that people shouldn't assume that Congress was acting in bad faith. He admitted that the Senate bill is "deeply flawed and unworkable," but said it was the best way to halt HR4437, the bill sponsored by Rep. James Sensenbrenner (R-Wis.).<p>
Oscar Chac&oacute;n of the NALACC argued that the movement needed to connect the street to the legislative debate and that lobbyists need to remember the demand of the recent demonstrations--legalization for all. He said that the Senate bill doesn't match our aspirations and should be rejected.<p>
The organizations demanding that the We Are America Alliance conference oppose the legislation were mainly grassroots organizations--including the National Day Laborers Organizing Network, Chicago's March 10th Movement, the Chicagoland Student Network for Immigrant Rights and the National Immigration Law Center.<p>
Labor representatives were divided on the question, although most Chicago union officials went to the "kill the bill" committee. This opposition points to the growing sentiment amongst activists and organizations across the country to oppose the "compromise" bill coming from Congress--and a growing confidence to stand up and say we deserve better.<p>
The conference voted to approve several actions, including a June 25 legislative day directed at Congress and the Labor Day regional demonstrations. <p>

