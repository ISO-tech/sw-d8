<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">June 9, 2006 | Pages 10 and 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Textileather</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Patrick Dyer</b></font><p>

<font face="Times New Roman, Times, serif" size="3">TOLEDO, Ohio--Some 285 workers at the Textileather Corporation went on strike May 30. The UNITE HERE Local 224T members make vinyl for automobile dashboards, armrests and sun visors. Workers, whose last three-year contract expired April 1 but was extended until May 10, rejected two offers last month.<p>
The company's latest contract offer would have required workers to change to an unwanted medical insurance plan or pay an increased premium on the plan they wanted. The offer also would have meant more nonunion contract workers taking over maintenance work. <p>
Don Yockey, who has worked at Textileather for 34 years, said the bosses' offer would push more maintenance work onto the machine operators and inspectors. Yockey said management never bargained in good faith, thinking that workers wouldn't strike. <p>
Management had the audacity to circulate leaflets in the plant telling employees that their negotiators weren't telling them the truth, <i>while the negotiating session was still going on</i>. Textileather's latest tactic is circulating threats in the media that workers will be sued for lost profits if they don't revote on the offer. <p>
Workers have experienced a steady increase in attacks on working conditions over the past several years. Terry Scott has worked at Textileather for 34 years, and says workers are treated so poorly that they were left with no choice but to strike. When Eddie Faust inquired about a holiday bonus last year, management told him that the T-shirt they gave each employee was their bonus.<p>
Union members have converted a house in the community into a strike headquarters, and picket lines will operate 24 hours a day, seven days a week.<p>
Currently about 60 supervisors are attempting to keep the plant running. Workers suspect that Textileather is planning to truck scabs into the plant. "People are just tired of their lies," Yockey said. "People here have had enough. We're fed up with being pushed around. We want respect."<p>

<font face="Times New Roman, Times, serif" size="4"><b>University of Illinois-Urbana Champaign</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Martin Smith</b></font><p>

<font face="Times New Roman, Times, serif" size="3">URBANA-CHAMPAIGN, Ill.--Library workers, nurses, telephone operators and firefighters represented by AFSCME Local 698 held a June 1 informational picket at the University of Illinois (UI) to protest a union-busting offer from management. The protest was one of the largest labor actions at the university in years, bringing together other campus unions and employees in solidarity with Local 698.<p>
Local 698 members have been without a contract since last August. The university has proposed a three-year contract with a 2 percent raise the first year, but the final two years would be tied to whatever UI gives academic staff and other employees based on its state funding--minus 1.5 percent.<p>
Union members voiced their disgust at the fact that their union is expected to receive less than the other campus employees. Likewise, with impending budget cuts from the state and the threat of inflation, the offer could impose a wage freeze.<p>
The tremendous solidarity shown by other campus unions at the picket will be critical to AFSCME's success.<p>

