<font face="Times New Roman, Times, serif" size="4">Their beating victim gets no justice in a Milwaukee courtroom</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The brutal police who walked free</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 21, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">AN ALL-WHITE jury let three former Milwaukee police officers go free in the vicious beating of an African American man.<p>
On the night of October 24, 2004, Frank Jude Jr., his friend Lovell Harris and two female college students were invited by an acquaintance to a party being thrown by police officer Andrew Spengler in Milwaukee's Bay View neighborhood.<p>
The party was filled with off-duty cops. According to the cops' version of events, Spengler says he caught Jude coming out of his bedroom, and realized that his police badge had been stolen. The cops at the party claim they were then forced to "subdue" Jude because he "resisted" arrest.<p>
But other witnesses tell a very different story of what happened. Both Jude and Harris are Black, while the two women they were with that night are white.<p>
According to Katie Brown, one of the women, when the four walked into the party together, the other guests--all of whom were white--stopped to stare at them. Kirstin Antonissen, the other woman, said that Jude seemed to overhear a racist remark.<p>
Feeling uncomfortable, the four left and got into Antonissen's pickup truck--which was suddenly surrounded by a dozen or more men from the party, screaming that they were cops. Jude was pulled out--by his legs.<p>
The terrified women called for help as Jude was surrounded, repeatedly kicked in the head and stripped naked in the street. One of the cops also pressed a knife up to Jude's throat.<p>
"I couldn't watch very much of it," Antonissen told the <i>Milwaukee Journal Sentinel</i>. "Men were just kicking him. You could see blood shooting out. I have never seen anything like it." "They had his arms behind his back the whole time," Brown said. "There was nothing he could do."<p>
No badge was ever found in Jude's possession, yet it was Jude who was initially arrested--even though he had been beaten so badly that he had to be hospitalized for two days. He suffered a concussion; a broken nose and fractured sinus cavity; cuts to both ears; cuts and swelling to his left eye, neck, head, face, legs and back; and a severely sprained left hand. Harris, who suffered a cut to his face, was also initially arrested.<p>
As news of the case broke in 2004--and with the threat of a major civil rights case looming--13 officers were eventually disciplined, with six being fired. But it took more than four months before three officers--Jon Bartlett, Daniel Masarik and Andrew Spengler--were finally charged with any crime.<p>
Defense attorneys were able to stack the deck in favor of the three white cops by striking every African American from the jury pool--claiming, for example, that the last two potential Black jurors were "not attentive" enough during jury questioning. In the end, every single juror was white--in an area that is 19 percent Black and 7.5 percent Hispanic.<p>
Jude was beaten so severely that he was unable to identify his attackers, and, predictably, most of the cops refused to talk.<p>
As District Attorney E. Michael McCann admitted to the <i>Journal Sentinel</i>, "This is not the usual type of case. Instead of police cooperating with you, you have a number of police who are intent on obstructing the inquiry."<p>
But during the trial, one officer, Nicole Belmore--who was on duty and one of the first to respond to the scene--testified that she witnessed three officers assault Jude. <p>
According to Belmore, Bartlett and Masarik kicked Jude while he was laying on the ground in handcuffs, and Bartlett put a knife to Jude's neck. "I heard Officer Bartlett say [to Jude], 'I'm going to kill you if you don't give me the badge,'" Belmore testified.<p>
She told the court that she witnessed Bartlett take a running kick at Jude's head--three times. Then, as Jude lay bleeding on the ground, "[Masarik] lifted Frank Jude's pants leg and started kicking him in the crotch, in the butt...I was mortified. I remember I pushed him and said: 'It's enough. Stop. It's over.'"<p>
Belmore says that she has been forced to retire because fellow officers carried out a campaign of harassment against her for speaking out. But that wasn't enough for the jury. <p>
Now, federal prosecutors say they are considering charging the officers in the attack--and angry residents are demanding justice.<p>
More than 500 people turned out last week for a meeting hosted by local Black ministers to demand an independent investigation. As Bishop Sedgwick Daniels told the Associated Press, "There is never any reason why the people we pay to protect our community...[should] feel like they have the right to kick and pull knives on you. We will not be silenced."<p>

