<font face="Times New Roman, Times, serif" size="4">More executions are halted</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Cruel punishment of lethal injection</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 21, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">A STRING of recent court rulings could stop the use of lethal injection by the U.S. death penalty machine. The rulings come in the wake of evidence suggesting that prisoners executed by lethal injection--a supposedly "painless" method--may, in fact, have endured agonizing deaths <p>
In North Carolina last week, U.S. District Judge Malcolm Howard told prison officials that they would need a medical professional during the execution of Willie Brown--slated for April 21--to make sure that Brown is sedated enough before paralytic and heart-stopping drugs are injected into his body. But the American Medical Association's code of ethics prohibits doctors taking an active role in executions, so it may be hard for North Carolina to find a willing accomplice.<p>
In February, a California judge ruled that a licensed anesthesiologist would have to oversee the execution of Michael Morales, or that the state would have to change the drugs used to kill prisoners. When doctors refused to participate, Morales' execution--and all executions in the state--were halted indefinitely.<p>
Several years ago, an appeals court halted executions in New Jersey pending an explanation from corrections officials about the state's lethal injection procedures. The New Jersey legislature adopted a one-year moratorium on executions this January.<p>
The U.S. Supreme Court recently stopped an execution in Florida to hear arguments that lethal injection, as currently practiced, constitutes cruel and unusual punishment.<p>
Three drugs are used in the lethal injection procedure. The first chemical, sodium thiopental, is a barbiturate used to sedate an inmate. The second drug, pancuronium, paralyzes the body. The third, potassium chloride, stops the heart.<p>
Lethal injection is the preferred method of execution in 35 states, and pro-death penalty forces have always maintained that the procedure is humane--similar to being "put to sleep." <p>
Leaving aside the torment that prisoners go through while waiting to be killed, and the fear and pain they undergo as intravenous lines are inserted, death penalty opponents point out that if sodium thiopental is improperly administered, it leaves the prisoner conscious throughout the execution, suffocating and in excruciating pain caused by the drugs, but unable to cry out because of drug-induced paralysis.<p>
A Florida study has shown low levels of anesthetic in the bloodstreams of executed inmates--suggesting that they were, in effect, tortured to death. Likewise, in North Carolina, Judge Howard based his decision on the fact that post-mortem levels of sodium thiopental in the bodies of four North Carolina inmates executed over the past six months suggest they might have been conscious as they were killed.<p>
Witnesses at lethal injections have frequently reported seeing prisoners in pain. In the North Carolina case, Judge Howard also noted that three lawyers who had witnessed executions in the state had seen condemned men writhing and gagging during their executions.<p>
"Instead of the quiet death I expected," one of the lawyers, Cynthia Adcock, said in a sworn statement about her client Willie Fisher, who was executed in 2001, "Willie began convulsing. The convulsing was so extreme that Willie's cousin jumped up screaming."<p>
The evidence is clear: There's no such thing as a "humane" execution.<p>

