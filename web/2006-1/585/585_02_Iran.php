<font face="Times New Roman, Times, serif" size="4">Denial of plans for a military attack on Iran ring hollow</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Credible threat" who sits in the White House</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 21, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE BUSH administration scrambled last week to quash reports that it was preparing to launch an attack on Iran--including the use of tactical nuclear weapons.<p>
President Bush called the reports--which grabbed headlines after a new expos&eacute; written by investigative journalist Seymour Hersh in the <i>New Yorker </i>magazine--"wild speculation." Defense Secretary Donald Rumsfeld called them "fantasyland."<p>
But considering all its saber-rattling against Iran, the administration's loud denials ring hollow.<p>
In his article, Hersh--basing his report largely on interviews with former intelligence officials and defense advisers--writes that the Bush administration, "while publicly advocating diplomacy in order to stop Iran from pursuing a nuclear weapon," has "increased clandestine activities inside Iran and intensified planning for a possible major air attack." He cites sources talking about the use of a bunker-buster tactical nuclear weapon to destroy Iran's main centrifuge plant in Natanz.<p>
Also in recent weeks, Vice President Dick Cheney and United Nations Ambassador John Bolton both appeared before the American-Israel Public Affairs Committee to promise "tangible and painful consequences," in Bolton's words, if Iran didn't halt its nuclear program.<p>
These threats aren't new. According to the <i>Washington Post's </i>William Arkin, "In early 2003, even as U.S. forces were on the brink of war with Iraq, the Army had already begun conducting an analysis for a full-scale war with Iran. The analysis, called TIRANNT, for 'theater Iran near term,' was coupled with a mock scenario for a Marine Corps invasion and a simulation of the Iranian missile force," Arkin reported on April 16.<p>
Around the same time, the U.S. and Britain conducted war games in the Caspian Sea, and Bush directed the U.S. Strategic Command to draw up a global strike war plan for an attack on Iranian weapons of mass destruction.<p>
In beating the war drums, Bush hasn't had to worry about any opposition from Congress either. In May 2004, the House of Representatives overwhelmingly passed a resolution authorizing Bush to use "any and all appropriate means" to prevent Iran from acquiring nuclear weapons. Since that time, Democratic leaders like Sens. John Kerry and Hillary Clinton have added their voices to the chorus.<p>
Bush singled out Iran, along with Iraq and North Korea, when he denounced all three as an "axis of evil" in his 2002 State of the Union address. So it's hardly "fantasy" to believe that Iran is on the U.S.'s short list of targets.<p>
Iran even tried to placate the U.S. by lending help in Washington's war on Afghanistan and offering to hand over al-Qaeda suspects, but it ended up on the list of targets anyway. That's because the Bush administration's war plans for Iran have nothing to with the threat of nuclear weapons--and everything to do with maintaining its dominance over the Middle East.<p>
The largest and most populated country on the Persian Gulf and the world's fourth-largest producer of oil, Iran is of huge strategic importance--especially considering the boiling crisis the U.S. has created in neighboring Iraq. If there's a "credible threat" in this story, it's the one that the U.S. poses to Iran.<p>

