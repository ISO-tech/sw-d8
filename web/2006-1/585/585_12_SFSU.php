<font face="Times New Roman, Times, serif" size="4">Students face repression for protesting recruiters</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Killing Iraqis is not a career"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Hector Agredano and Kristin Lubbert</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 21, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">STUDENTS IN Northern California are standing up to military recruiters--and facing repression from school administrators for exercising their right to protest.<p>
On April 11, four military recruiters for the Army and National Guard left a job fair at the University of California-Santa Cruz (UCSC) after a protest of approximately 100 students, organized by Students Against War (SAW). But before the recruiters packed up and left, a campus police officer and a university official physically assaulted two students as they attempted to leave the room. <p>
Meanwhile, at San Francisco State University (SFSU), a group of activists were charged with disturbing the peace and received 14-day suspensions from campus after holding a counter-recruitment protest on April 14.<p>
Last December, SAW at Santa Cruz made national headlines when a leaked Pentagon document revealed that the government had been keeping tabs on antiwar events and counter-recruitment protests. SAW was labeled a "credible threat" by the Pentagon.<p>
Instead of intimidating students, however, the revelation helped inspire a new wave of campus activism.<p>
At a morning rally before the April 11 job fair, students spoke out against the war in Iraq, the tide of racism against Arabs and Muslims, the scapegoating of immigrants and the recruitment of students from working-class families to fill the military ranks. Later, students marched to the career fair chanting "Recruiters are not welcome here--killing Iraqis is not a career!" and gathering more protesters along the way.<p>
"What happens at the UC does matter," UCSC senior and SAW member Marla Zubel told the crowd, as activists blocked the doors to the room where the recruiters were set up. "That's why they're not leaving. Because they know if they leave that sets a precedent, and other campuses will have ways to kick them off too, and it will be like a shock wave throughout the country."<p>
After an hour and a half of negotiations between students and administrators, it was announced that the recruiters would leave. But when they didn't, a group of 10 female protesters entered to demand that recruiters get out because of the military's sexist history.<p>
This is when the two students were assaulted--and the military finally left, escorted through the back door by several university officers. As students saw the recruiters escaping, they chanted, "Don't come back! Don't come back!"<p>
At SFSU, on April 14, a group of SFSU Students Against War activists rallied in Malcolm X Park before proceeding to the campus career fair where recruiters had set up shop.<p>
Counter-recruiters lined up behind ropes set up by the school around the recruiters' table--and asked the military representatives questions like "Why are a third of homeless people veterans?" Students stood near the recruiters, chanting--until campus police broke up the protest. <p>
The 10 activists charged were initially told that if they were caught on campus during their suspensions, they would be fined $500. Two of the students who live on campus were told that they weren't allowed to go to the dormitories they pay to live in. Outrageously, the students were also told that they couldn't contest the charges against them--because the office that would handle the matter is booked through the end of May. <p>
But students are refusing to stay quiet as the administration violates their rights. At an April 17 press conference, the students denounced the university's threats against them--only to later find letters at their dorms from SFSU President Robert Corrigan, threatening them with arrest if they set foot on campus before April 28.<p>
SFSU administrators later backed off their threats, saying that the students wouldn't be arrested if they returned to campus. But the threat of internal disciplinary action--against both individual students and SAW--remains.<p>
That's why we need to continue the fight to defend our right to protest military recruiters on our campuses.<p>
