<font face="Times New Roman, Times, serif" size="5"><b>Cindy Sheehan returns to Camp Casey</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Cindy Beringer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 21, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">WHEN CINDY Sheehan re-opened Camp Casey in Crawford, Texas, over the Easter weekend, President Bush retreated to Camp David. "We sent him into hiding," Sheehan said.<p>
The weekend's activities began April 13 when antiwar campers from around the country were joined by the March to Redeem the Soul of America. Marchers began their 120-mile trek with an April 1 protest at ExxonMobil headquarters in Irving, Texas, near Dallas.<p>
The marchers, whose numbers ranged between six and 25, made several stops to speak to people and the press along the way. William Jones, a 16-year-old student from Austin who wanted to show his opposition to the war, made the entire trip.<p>
Nick Mottern, the national director of the March to Redeem the Soul of America, said that at least 60 percent of the people they met along the way supported the marchers. However, Mottern felt that "as a viable political force, the peace movement is non-existent at this point," adding that "unless people are willing to get out in the streets in great numbers, we won't change anything."<p>
About 400 people attended a rally at Camp Casey on April 15, which included speakers from Gold Star Families for Peace, Military Families Speak Out, Iraq Veterans Against the War, and Veterans for Peace. The Cathedral of Hope in Dallas brought a busload of 60 gays and lesbians who oppose the war. <p>
When Rev. Peter Johnson said, "We need to combine our movements," patches of the crowd broke into a spontaneous chant of "S&iacute;, se puede," (Yes, we can!), acknowledging the power and energy of the immigrant-rights movement.<p>
"The warmongers are elitists, racists, sexists and homophobes," said Rev. Joseph Lowery, a veteran of the civil rights movement and one of the original founders of the Southern Christian Leadership Conference, who's now 84 years old. "So how can we allow ourselves to be divided? They want to hijack our movement" and place it on the "rotunda of celebratory irrelevance" on which they placed Dr. Martin Luther King.<p>
Army veteran Geoffrey Millard spoke about military families torn apart by divorce as soldiers come home different people than the ones who had left. Sheehan closed the rally by speaking about her frustration with Democrats who remain silent on the war. <p>
On April 17, Sheehan and others from Camp Casey came to Austin for several events at the University of Texas, including a protest that included Sheehan, Col. Ann Wright, Juan Torres, Hart Viges and Dana Cloud of the ISO. A small group of counterprotesters set up by the Young Conservatives of Texas (a racist and xenophobic group) were shouted down by the antiwar crowd.<p>
Protesters then marched through the campus and into the U.S. Army recruiting station, halting activity until the police arrived. "Our country is in the hands of maniacs, the media isn't doing its job, and Congress is irrelevant," said Sheehan later that evening at a panel discussion at UT. "We have to be the checks and balances."<p>
<i>Robert McDonald contributed to this report</i>.<p>

