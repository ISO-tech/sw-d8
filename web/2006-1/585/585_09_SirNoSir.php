<font face="Times New Roman, Times, serif" size="4">Film chronicles the GI resistance during Vietnam</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The soldiers who fought the war</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Michael Hoffman</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 21, 2006 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Sir No Sir! The Suppressed Story of the GI Movement to End the War in Vietnam, </b></i>produced, directed and written by David Zeiger.</font><p>

<font face="Times New Roman, Times, serif" size="3">THERE IS a myth of the "spat upon Vet." The tale goes that the soldiers of Vietnam came home to a radical fringe movement that spat on them as they unloaded onto the tarmac. This myth has served to all but erase the real history of the Vietnam antiwar movement from U.S. consciousness.<p>
David Zeiger's film <i>Sir No Sir! The Suppressed Story of the GI Movement to End the War In Vietnam</i> lays this myth completely to rest by telling the inspiring story of a movement that shook the very foundations of U.S. military power.<p>
Through interviews and stock footage, this powerful documentary tells the story of how thousands of U.S. soldiers--through their first-hand experience on the ground--came to realize that the war in Vietnam was an unjust travesty.<p>
The film opens with soldiers who joined the Army ready to "serve their country" but later came to actively oppose the U.S. military endeavor.<p>
Starting in 1965--just a few years after the start of the ground assault in Vietnam--a handful of soldiers began refusing orders and seeking conscientious objector status. The response of GIs to the military's repressive treatment of these early objectors helped to reveal the widespread discontent within the military, foreshadowing the tremendous upsurge to follow. <p>
The glaring hypocrisy of U.S. policy in Vietnam became increasingly apparent as the war escalated. By 1968, there were dozens of underground newspapers, antiwar coffeehouses for active GIs, and a variety of antiwar organizations founded specifically for soldiers and veterans.<p>
The incident that puts the GI movement into the national spotlight is the emergence of the "Nine for Peace," nine active-duty officers who released a pamphlet announcing their opposition to the war and their resignation from the military. In mid-July of 1968, the nine staged a 48-hour protest where they chained themselves to priests and marched to several churches in San Francisco.<p>
They were eventually arrested and sent to different military prisons around the country. In San Francisco, one of the Nine for Peace, Bill Mather, was sent to the Presidio Stockade where he faced intensely overcrowded, degrading and unsanitary conditions, laying the basis for the next major upturn in the GI movement.<p>
On October 14, 1968, prison guards shot and killed a young mentally disturbed soldier while he was trying to escape. Outrage swept the prison and the next day 27 prisoners broke ranks and staged a sit-in on the yard.<p>
They were afterward dubbed the "Pentagon 27," and hundreds of protesters showed up in solidarity.<p>
This, the first major act of organized resistance inside the military, launched a new wave of GI protest where active and AWOL soldiers began to feel their power and take the lead in a movement to end the war.<p>
The naked hypocrisy of the U.S. government is a theme that runs throughout the entire film and is given repeatedly as the reason why the GI movement spread to all branches and ranks of the military.<p>
The racism rampant in U.S. society and the confident lead of the Black Power movement gave way to an especially powerful resistance on the part of Black soldiers.<p>
As one Black soldier says to a crowd of his comrades, "The only place where a Black man should fight is where he is oppressed." In another scene we hear a Black veteran describing how he felt when he realized that the word "gook," used profusely in the military, was actually a racial slur for the Vietnamese--much like the slurs used against Blacks in the U.S.<p>
The disintegration of military discipline on the part of combat troops is exposed in the film as a major cause of the policy shifts taken by the U.S. "Vietnamization" is seen as a result of this; the U.S. was forced to rely on brutal air strikes to maintain its grip on the region.<p>
This only served to deepen the resistance of enlisted personnel as Naval crews and Air Force intelligence officers refused to be a part of the bloodshed.<p>
As the crisis for the U.S. deepened, the desperation of their military tactics and the brazenness of their lies was reciprocated by the men and women ordered to carry out the warfare with an increasingly bold resistance. <p>
At the opening night of <i>Sir No Sir! </i>in San Francisco, two vets featured in the film addressed the crowd of some 200 people with a call for solidarity. "We want this film to be seen all over the U.S. and we need your support to make this happen," said Michael Wong.<p>
It's a must that every person seeking social change go out and see this film. Its success in its initial showings will determine the length and breadth of its time in theaters.<p>
Go to www.sirnosir.com to find local show times. Posters and T-shirts are also available to support the cause. Anyone currently in the military can receive a free DVD from the organization Courage to Resist.<p>
So, spread the word. It is the job of everyone against the current U.S. war in Iraq to help build a movement that embraces GI resisters and takes a stand against U.S. aggression.<p>

