<p><a name="Twice"></a><font face="Times New Roman, Times, serif" size="4"><b>Think twice before striking</b></font><p>

<font face="Times New Roman, Times, serif" size="3">TO THOSE workers at Delphi who think they are being taken advantage of, I would suggest they think twice before they plunge themselves into a prolonged strike in a battle over wages and benefits.<p>
The financial difficulties at General Motors, and the U.S. auto industry in general, are real. Past labor agreements, made in haste and which did not anticipate future economic challenges facing the industry today, have forced changes on GM which have included massive plant closures, cutbacks in benefits of retirees and white-collar workers, the elimination of entire model lines, and the sale of once-profitable subsidiaries. Foreign competition, rising health benefits and energy costs represent just some of the changes imposed on the industry.<p>
As one who works in an automobile related field, I have seen my own wages and benefits decline, as the cost of everything rises. Still, I do not believe that adding to the industry's challenges by placing unrealistic demands upon companies will advance the interests of American auto workers. <p>
The most likely company reaction will be further downsizing, sending more jobs overseas and more layoffs. Auto industry workers will then face the prospect of working at service-sector jobs for low salaries. Unless or until the industry is able to regroup, and operate profitably, we all face difficult choices.<br>
<b>Eric Stefik,</b> Las Vegas, N.V.<p>
