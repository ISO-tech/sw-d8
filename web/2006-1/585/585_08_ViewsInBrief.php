<font face="Times New Roman, Times, serif" size="4"><b>La lucha seguira</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AS HUNDREDS of students converged at Park Street and West Johnson in Madison in support of immigrant rights April 10, thousands of people awaited their arrival at Brittingham Park. <p>
Police coordinated the blocking of traffic as students marched the mile from campus to the park to the beat of their makeshift drums--five-gallon buckets hanging from string around their rebellious necks, covered with stenciled images of famous revolutionaries--chanting "Aqui y alla, la lucha seguira!" (Here and there, the fight will continue!).<p>
All the students' organizational efforts, combined with that of local unions, human-rights groups, churches and the Latino community paid off as thousands, disgusted with the U.S. Congress and their racist policies proceeded the mile down W. Washington Ave. to the Capitol building to express their discontent. Many stared in awe as this gigantic following filled the streets, and clogged up traffic for nearly an hour. <p>
Although five or six skinheads came out to "respect our laws and our orders," they were chanted down and chased away, and hardly affected the high energy levels in the air that was filled with the shouts of those that did not consider any part of those laws their own. The Capitol building was bombarded with thousands of workers, united against oppression, many of whom took the day off of work, excused their children from school and left their mark not only in their presence, but also in their absence.<br>
<b>Lauren Schmidt,</b> Madison, Wis.<p>
