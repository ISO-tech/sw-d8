<p><a name="A"></a><font face="Times New Roman, Times, serif" size="4"><b>A is for antiracist</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BEN DAVIS is not the first comrade I've heard complain about the anarchist subtext in <i>V for Vendetta</i>, but frankly, I don't get it ("A is for anarchism," April 14). <p>
When a mainstream movie takes a stand against homophobia, anti-Arab racism, government repression and surveillance, and then calls for the revolutionary overthrow of that government, I don't see how socialists do anything but cheer. When a movie takes an uncompromising position of supporting the violence of the oppressed against the much greater violence of the oppressor, how can socialists be anything but overjoyed? <p>
Additionally, <i>V for Vendetta</i> does show regular people taking a stand against their government in response to a "fingerman" killing a neighborhood youth. Far from an elitist view that people are sheep, the movie is clear to identify that people don't buy all the crap their government tells them, even if they are too fearful or lack the confidence to directly defy it.<p>
In the event that people who now join the International Socialist Organization start asking "Where are your masks?" well, I suppose a little patient explaining will be in order. Barring that, I say lay down $10 of capital to see <i>V for Vendetta,</i> and then join the nearest "Day Without an Immigrant" rally on May 1.<br>
<b>Andrew Libson,</b> San Francisco<p>

