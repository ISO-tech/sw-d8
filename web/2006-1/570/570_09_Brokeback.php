<font face="Times New Roman, Times, serif" size="5"><b>Love and repression in the American West</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Jeff Bale</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 6, 2006 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Brokeback Mountain, </b></i>directed by Ang Lee, starring Jake Gyllenhaal and Heath Ledger.</font><p>

<font face="Times New Roman, Times, serif" size="3">IT'S RARE that a film lives up to the media hype that precedes its release--rarer still when the film is so much better than promised. Yet <i>Brokeback Mountain</i> manages to do both.<p>
The movie, directed by Ang Lee and based on a short story by Annie Proulx that first appeared eight years ago in the <i>New Yorker</i> magazine, tells the story of an ill-fated romance between two ranch hands in Wyoming in the 1960s and beyond. <p>
Jack and Ennis meet when they're sent to herd sheep for the summer on the remote hillsides of Brokeback Mountain, Wyoming. Their friendship enters new territory after a night of drinking when the two have sex.<p>
Director Lee shies away from nothing in capturing the contrasts between the roughness of their act and the tenderness that lies just beneath the surface. That tension remains throughout the summer, as the two fall in love--even though both have pronounced to the other that they aren't "queer." Again, though, Lee allows the romance between them to flourish on the screen--set against the majestic background of the Rockies.<p>
Their summer together is cut short by an impending blizzard. Ennis returns home, marries his girlfriend and settles into a run-down apartment over the town laundromat to raise their two daughters. Jack works the rodeo circuit until he gets to Texas, where he meets Lureen, the daughter of a successful farm equipment salesman. The two marry, have a son and open up a farm equipment dealership.<p>
Lee powerfully contrasts the closed, constricted, pained lives that Jack and Ennis now lead with the expansiveness of the wildlife scenes where the two met and fell in love.<p>
Four years later, Jack makes his way from Texas to Ennis' hometown in Wyoming, where the two meet again in a scene overflowing with passion and fear. They leave for a "fishing trip," the first of what will be 20 years of getaways to return to the love they share for one another, one that endures divorce, hardship, death and disappointment.<p>
The movie is so much better than the media campaign around it. Although the studios have cynically emphasized the movie's "universal" theme while backing off its gay content (let alone the nudity and love-making), the fact is that the story, the director and the actors delve head-first into the complexity and despair of same-sex love in the Wild West, before anyone heard of a "gay-rights movement."<p>
Much ink has been spilled in reviews of <i>Brokeback Mountain </i>about the gay subtexts in classic Western movies and the archetypal figures, like John Wayne, who star in them. Perhaps Lee knew this discussion would surround the movie. In one scene at a Fourth of July event in Ennis' Wyoming town, Lee seems to fun at the patriotism and machismo of these earlier movies.<p>
Two men sit behind Ennis and his wife and are cursing in the foulest terms as they swap swigs of whiskey. Ennis gets up to defend his family's honor by beating the two up as American flags wave wildly and fireworks explode in the air behind him.<p>
To be sure, there is no overt political message in this movie. It is, after all, an engrossing love story. But even though Jack and Ennis' relationship is front and center in this movie, Lee does not let the social context in which it is taking place off the hook.<p>
This is particularly important in that the story unfolds in the same place where Matthew Shepard was brutally murdered seven years ago, and even more important in today's world, where attacking same-sex marriage and gay people is the rallying cry of a venomous Religious Right movement.<p>
Lee doesn't make the same mistake of other recent gay-themed movies in pushing the social and political context in which the story takes place outside the camera's view. Instead, that social and political context is allowed to fuel four gut-wrenching performances in a movie that is sure to become a classic.<p>

