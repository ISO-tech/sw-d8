<font face="Times New Roman, Times, serif" size="5"><b>Marines detain Vietnam resister</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 6, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE MARINES have decided to prosecute yet another war resister. But this case has a twist: It's 40 years old. Jerry Texiero refused orders to deploy to an illegal and unjust war half a world away--not in Iraq or Afghanistan, but in Vietnam.<p>
Jerry refused combat in July 1965. For more than 40 years, he ran a classic car business in Florida. As the result of a larceny conviction stemming from the corrupt dealings of a business partner, a unit charged with apprehending Marines who don't turn up for duty recently matched his current whereabouts to his military file.<p>
Local police arrested him August 14, 2005, and in December, he was transferred to a Marine brig at Fort LeJeune, N.C. The Marines are now contemplating whether to court-martial Jerry for desertion, which carries a possible five-year prison term.<p>
"Why are scarce Marine resources being squandered on the prosecution of a senior citizen whose only 'crime' is refusing to fight a war that today is universally discredited?" asked Citizen Soldier legal director Tod Ensign in a letter to Camp LeJeune base commander Major Gen. Robert Dickinson. "Or is the Corps warning Marines in Iraq that they will pursue deserters to the grave?"<p>
In the coming weeks, Vietnam veterans and others plan to demonstrate in support of Jerry on or near Camp LeJeune.<p>
