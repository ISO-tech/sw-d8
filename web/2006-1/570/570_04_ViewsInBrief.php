<p><a name="Iran"></a><font face="Times New Roman, Times, serif" size="4"><b>Iran and the nuclear threat</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SO BUSH and Blair are worried by Iran's nuclear potential. Well, let's have a look at things from Iran's perspective: To their north lies Russia--a nuclear state that has invaded Muslim Chechnya and fought numerous skirmishes with other former USSR republics. Militaristic, expansionist, aggressive.<p>
In the west is Iraq--a country that invaded Iran in the 1980s and is now occupied by two hostile powers, the UK and U.S., both of whom have invaded other countries in recent years and are also both nuclear armed. Also in the west is Israel--again, a nuclear power and again openly hostile.<p>
To the south, Saudi Arabia. The "homeland" of al-Qaeda and highly unstable. And, more importantly from Iran's strategic perspective, the location of U.S./UK strike airbases.<p>
In the east, Pakistan and India, both now nuclear powers, and also Afghanistan, the "playground" of both al-Qaeda and the Taliban, and again the location of hostile Western military bases. And China--a country that is not only nuclear, but thought nothing of liquidating 3,000 of its own citizens in Tiananmen Square.<p>
From Iran's point of view, it would be positively irresponsible not to acquire the bomb. They won't survive as an independent nation without it.<br>
<b>Ad Williams,</b> Anglesey, United Kingdom<p>

