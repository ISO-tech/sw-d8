<p><a name="WR"></a><font face="Times New Roman, Times, serif" size="4"><b>War resisters on film</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AS THE movie trailer says, <i>Sir! No Sir!</i> is "the story of one of the most vibrant and widespread upheavals of the 1960s--one that had a profound impact on American society, yet has been virtually obliterated from the collective memory of that time." <p>
But this movement wasn't in the streets or on "college campuses, it was in barracks and on aircraft carriers."<p>
<i>Sir! No Sir!</i> uses archival footage intertwined with contemporary interviews to retell the story of a resistance movement among American GIs, whose vibrancy and defiance challenged the power structure of the U.S. military. <p>
This resistance, caused the <i>Armed Forces Journal</i> in June 1971, to report: "[O]ur army that now remains in Vietnam is in a state approaching collapse, with individual units avoiding or having refused combat, murdering their officers and noncommissioned officers, drug-ridden, and dispirited where not near-mutinous...[C]onditions [exist] among American forces in Vietnam that have only been exceeded in this century by the French Army's Nivelle mutinies of 1917 and the collapse of the Tsarist armies in 1916 and 1917."<p>
This story is a piece of our history actively suppressed by those fearful of the power the GI resistance movement commanded and the lessons it taught. <p>
The classic opening sequence of a filmed U.S. bombing run reminds you that this documentary isn't of the Discovery Channel variety. Numerous interviews from soldiers and participants provide a continuous narration reminiscent of Michel Moore's <i>Roger and Me.<p>
</i>Starting with Sgt. Donald Duncan, one of the first cases of open soldier resistance, all the way through the Winter Soldier hearings, we experience the many challenges and rejoice in the victories as the GI resistance grows throughout the film. <p>
In one of the most compelling sequences, one of the Presidio 27, a group of war resisters imprisoned in San Francisco, remembers with astonishment, "I came in as an AWOL, and within two days of hitting the stockade, I was facing the death sentence for singing, 'We Shall Overcome.'"<p>
His account not only highlights the seriousness of the situation, but also alludes to something else. The Black Power movement spread like wildfire among the ranks of Black and white soldiers. Many saw the images of dogs being used on civil rights protesters and drew the conclusion that legendary boxer Muhammad Ali expressed so well: "Ain't no VC ever called me nigger."<p>
We are treated to a long segment examining soldier newspapers (numbering in the hundreds), which accompanied the "coffeehouse movement," giving ordinary GIs a forum for dissent and a vehicle for organizing within their ranks. And we learn about the massive support that existed among ordinary U.S. citizens for GIs who stood up, countering the myth of antiwar protesters spitting on returning soldiers.<p>
One criticism of this documentary would be the scant attention paid to the political conclusions soldiers drew from their experiences. Many left for military service as loyal patriots and came back as radicals--and some as revolutionaries who then became leaders in the broad upheaval that shook the nation in the early 1970s. This is a story that also needs retelling.<p>
The most important message of this film is that of the real American GI. The U.S. media would have us believe that today's soldier is a loyal, unquestioning martyr, sacrificing his or her individuality and obeying orders that, ultimately, are for the greater good of America. We are told to honor these gladiators, while Bush and the military brass grind them into dust. <p>
<i>Sir! No Sir!</i> reminds us of why resistance to the crass, greedy elite that run our society is always possible. We learn of men and women in uniform who sympathize with human suffering, grapple with the contradictions of war, and are capable, given the right circumstances, of refusing their orders to fight in wars that benefit no one but the wealthy. <p>
As the socialist playwright Bertolt Brecht wrote: "General, man is very useful. He can fly and he can kill. But he has one defect: He can think."<p>
Go see this movie. Show it on your campuses, your union halls and your community centers. We owe it to the veterans and activists who built the GI resistance movement then, and we owe it to ourselves, to help build it again. <p>
As the popular chant goes, "They're our brothers, they're our sisters, we support war resisters!"<br>
<b>Brian Lenzo,</b> Rochester, N.Y. <p>

