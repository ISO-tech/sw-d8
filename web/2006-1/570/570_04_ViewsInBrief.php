<font face="Times New Roman, Times, serif" size="4"><b>"Labor needs a victory"</b></font><p>

<font face="Times New Roman, Times, serif" size="3">"LABOR NEEDS a victory!" one transit worker told left-wing radio station WBAI. "We're trying to raise the standard of living for all workers in New York."<p>
So many of my coworkers and family said that the transit workers are asking for "too much" because many New Yorkers don't make the wages and get the benefits that transit workers already have. However, a transit workers' strike is high profile, and they may give confidence to working-class New Yorkers to fight and win better working conditions and pay.<p>
Metropolitan Transit Authority Chair Peter Kalikow called the strike "a slap in the face" to all New Yorkers, but he's wrong. It's a well-deserved slap in his face and in the face of <i>wealthy</i> New Yorkers.<br>
<b>Dominic Renda, </b>New York City<p>

