<font face="Times New Roman, Times, serif" size="5"><b>UAW rebels keep up the pressure</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 6, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">THE FIGHT by rank-and-file activists in the United Auto Workers (UAW) against concessions is heating up amid plans for a big march at the Detroit Auto Show January 8 and a dispute over last month's vote that allegedly ratified union concessions at Ford.<p>
Although the UAW International reported that 51 percent of union members voting approved massive health care concessions at Ford and parts maker Visteon, workers at big UAW locals voted down the deal by big margins--including at the Ford City complex in Louisville, Ky., the home local of UAW President Ron Gettelfinger.<p>
Opposition in the UAW has been stirring since October, when General Motor's former parts division, Delphi, declared bankruptcy and announced plans to cut wages from about $24 to $26 to per hour to about $10. The move is on hold pending hearings before a federal bankruptcy court judge, set for February in New York City.<p>
In the meantime, rank-and-file meetings in Grand Rapids, Mich.; Kokomo, Ind.; Flint, Mich.; and St. Louis, Mo., have attracted hundreds of workers opposed to cuts--which included increases in retiree health care costs to GM retirees worth $1 billion per year. GM went on to announce the elimination of 30,000 jobs, a figure matched by Ford soon afterward.<p>
Thus when Ford and Visteon locals voted on retiree health care cuts that would save Ford $850 million per year, longtime UAW dissidents who had been relatively isolated in recent years were able to mobilize successful "no" votes in key locals.<p>
"At Ford, 51 percent is as narrow as it can get, which is a clear indication that there is a lot of dissatisfaction" in the UAW, said Gregg Shotwell, a member of Local 2151 at the Delphi plant in Coopersville, Mich., and a key organizer in the new Soldiers of Solidarity UAW network. "A lot of this opposition stems from the way ratification vote was held. In some locals, the information meetings were held on the same day as the vote, and people felt there was a lack of information. Also, the procedure didn't have accountability. They had bargaining committees counting ratification votes, which is a conflict of interest."<p>
Rank-and-file activists are also critical of the UAW for caving on the issue of health care rather than trying to generalize the fight over that issue. "This was a big opportunity for the UAW to act like leaders of the labor movement," Shotwell said. "The only viable social and economic solution is national health care."<p>
The rank-and-file UAW meetings--the biggest such gatherings in the union in more than 20 years--have put pressure on Gettelfinger and the UAW International leadership, whose past promises to protect jobs by taking concessions have been repeatedly broken.<p>
In the past, UAW leaders were able to ride out criticism by making job losses gradual--allowing management not to replace retiring workers and allowing laid-off workers to receive pay while they wait in a jobs bank for a new job to open up. However, the Delphi bankruptcy and GM's big losses have accelerated demands for more cutbacks.<p>
What's more, the possibility of a decent retirement is no longer as great a lure after retiree health care cuts and Delphi CEO Robert "Steve" Miller's efforts to dump the company's pension obligations through bankruptcy courts.<p>
"I think Delphi had a business plan to go bankrupt, but it got out of hand and went faster than they wanted, and it's become very messy," Shotwell said. "Also, Miller was very cocky. He was very antagonistic, and thought he was going to roll over the UAW. And the UAW International probably hoped that we would scare us bad enough so that we would roll over for concessions. Instead, the UAW rank-and-file response has been outrage and a willingness to fight back."<p>
Many UAW dissidents were inspired by the recent New York City transit strike, Shotwell said. "People I talked to were impressed that people were willing to defy the law and their international to protect new hires. They were very impressed that they had the courage to do that, because we may be in a very similar situation."<p>

