<font face="Arial, Helvetica, sans-serif" size="2"><b>OBITUARY</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Clinton Jencks</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 6, 2006 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3"><i>SALT OF the Earth, </i>which tells the story of Mexican-American miners striking in New Mexico during the McCarthy-era 1950s, has long been considered a must-see for anyone fighting for a better world.<p>
On December 14, Clinton Jencks--a principal player in the movie and the real-life struggle in the mines--died at the age of 87.<p>
Jencks, an organizer for the International Union of Mine, Mill and Smelter Workers, and his wife Virginia were at the center of the 15-month strike against Empire Zinc in Bayard, New Mexico, beginning in 1950.<p>
Unlike most of the main trade unions, the Mine-Mill union prioritized the struggles of minority and immigrant workers. In 1950, the Mine-Mill union and 10 other unions were targeted for having ties to the Communist Party and were purged from the Congress of Industrial Organizations.<p>
During the Empire strike, the bosses would also use anti-communism to try to weaken solidarity, telling workers that they were the dupes of communists. This flew in the face of workers' experience, as rank-and-file workers took part in making day-to-day decisions and served in the leadership, in which often the best fighters were communists.<p>
And when blacklisted filmmakers Paul Jarrico and Herbert Biberman set to work putting the story to film, they involved strikers in every stage of the process--from okaying the screenplay to playing all but a few of the parts. So when you see the famous scenes of miners' wives fighting the cops on the picket line, you're seeing the real women who did the fighting.<p>
Like so many other radicals during the McCarthy-era red scare, Jencks became the target of government repression. A government informer, who later admitted he'd lied, claimed Jencks was a member of the Communist Party and therefore had perjured himself by signing an anti-communist loyalty oath in 1950.<p>
Despite a spirited defense campaign launched by the Mine-Mill union and the <i>Salt </i>crew, he was sentenced to five years in prison in 1954. Jencks was freed on appeal and finally exonerated in 1957 but, by that time, was considered "politically unemployable," in the words of one California employment official. Later he earned a doctorate in economics and taught at San Diego State from 1964 until he retired in 1988.<p>
As the U.S. government conducts a modern scare of its own and solidarity is at a premium, it's a good time to remember radicals like Jencks, who knew what it meant to stand up.<p>

