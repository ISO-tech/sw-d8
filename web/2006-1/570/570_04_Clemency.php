<font face="Times New Roman, Times, serif" size="5"><b>Why clemency is a joke</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 6, 2006 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">IN THE 1990s, Public Enemy recorded "911 Is a Joke" because the emergency number didn't work for poor African Americans who called it in order to get help.<p>
Recently, FEMA, under the Bush administration and under the leadership of its then-director Michael Brown, showed the world during Hurricane Katrina that it, like 911, is a joke. It was not there for the poor people who needed it, though it was supposed to be.<p>
This seems to be the case more often than not when it comes to life in America for poor people--especially poor African Americans. Because this is a fact of life, it should not be a real surprise that clemency in this country and within this state--when it comes to poor people and particularly poor Black males--is a joke.<p>
If any person deserved clemency, it was Stanley Tookie Williams, and the reasons why are obvious! Yet the powers that be in California have shown that--like 911 and FEMA--they will not be there for the poor folks who called upon them to save a life.<p>
It now appears that the only person who may ever get clemency in California is the son or daughter or relative of a rich family, should such a person somehow happen to get sentenced to death row in the first place. <p>
Then the behind-the-scenes favors will begin to take place--as they did for Patty Hearst. She didn't go to death row, but to everyone's surprise she did get convicted and sent to jail. Then what happened? Somewhere down the line, she got pardoned, which is a form of clemency. Yet none of the poor people who were convicted with her got pardoned.<p>
911, FEMA, clemency and every other program in this country work for the rich and powerful--the people who need them the least. This doesn't seem to bother most people. In fact, many poor people say that's just the way it is. And that's a shame, because it's real life and that's no joke!<p>
In struggle from death row,<br>
<b>Kevin Cooper, </b>San Quentin, Calif.<p>

