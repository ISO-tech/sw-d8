<font face="Times New Roman, Times, serif" size="5"><b>High stakes in Teamster vote</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Joe Allen, Teamsters Local 705</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 6, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">THE YEAR ended with increasingly bad news for the reelection campaign of Teamster General President James P. Hoffa.<p>
The <i>Detroit News </i>on Christmas Day ran an extremely critical article headlined, "Hoffa faces tough re-election drive," which went on to list the myriad of issues dragging down the embattled union president. Hoffa's problems range from his failure to end the federal government's oversight of the union to the near bankruptcy of Central States Pension Fund, the Teamsters' largest.<p>
Such media coverage, especially from the hometown newspaper of the Hoffa family, is virtually unheard of--and is the latest blow to the carefully crafted public image that Hoffa and his staff have built over the years.<p>
James P. Hoffa--son of the notorious and mob-murdered union president of the 1950s and 1960s, Jimmy Hoffa--is running for his second full-term in office. Teamster members will elect the top officers of the union through mail-in ballots in December 2006.<p>
Hoffa came to power in the aftermath of a federal government sponsored witch-hunt that drove from office the Teamsters' first reform president, Ron Carey, in 1998. Carey had garnered the wrath of big business and its Republican allies in control of Congress by the leading the most important strike in nearly a generation, the 1997 strike against package delivery behemoth UPS.<p>
"Restoring Teamster Power" and "Unity," the catch phrases and buzzwords that have splashed been across Hoffa campaign literature and union press releases, have lately become something of a joke. Two top Hoffa allies, who currently serve as International vice presidents, have pulled out of his election slate.<p>
New York Teamster officer Tom O'Donnell last fall announced that he would be running against Hoffa. He was joined temporarily by Tyson Johnson, a Texas Teamster officer who had been appointed freight director by Hoffa. Johnson, however, later met with Hoffa and rejoined his slate--but after seven years in office, the union is increasingly fractured.<p>
By contrast, the transportation and freight industries--still the core of the 1.4 million-member union--are becoming consolidated into a handful of powerful giants operating on a global scale. UPS, already the world's largest package delivery company, announced last spring that it was purchasing the viciously anti-union Overnite in a bid to be a major player in the freight industry.<p>
The Hoffa administration seems befuddled in the face of these historic changes and has been swamped instead by corruption scandals involving Hoffa's closest advisors.<p>
The best bet for still beating Hoffa and changing the direction of the union rests with well-known reformer, Tom Leedham, from Portland, Ore., who announced his candidacy on October 31. Leedham ran against Hoffa in 1998 and 2001 with the support of Teamsters for a Democratic Union (TDU), the longstanding rank-and-file reform group that is set to back this year's campaign. <p>
"Hoffa has not kept a single promise he made to working Teamsters," Leedham said. "They are fed up. They want an honest union that is not about flashy public relations and rhetoric."<p>
Supporters of Leedham's Strong Contracts, Good Pensions Slate shocked Hoffa supporters by winning all seventeen delegate spots in Teamster Local 89, one of the largest Teamster locals in the country and representing the mammoth UPS air hub in Louisville, Ky. They defeated a delegate slate represented by Hoffa-appointed Carhaul Division Director Fred Zuckerman.<p>
Hoffa, however, is far from finished. He still represents the bulk of the officers the union, has plenty of cash and is prepared to use every dirty trick to stay in power.<p>
But for Teamster reformers, the 2006 election represents the best opportunity in a decade to win the top posts in the union.<p>

