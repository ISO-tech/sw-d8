<font face="Times New Roman, Times, serif" size="4">Alito likely to sail through hearings</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Dream come true for the right wing</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 6, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">CONFIRMATION HEARINGS will begin early in January for Supreme Court nominee Samuel Alito, and the Christian Right believes he will be their dream come true if confirmed.<p>
In addition to his past ruling in favor of the right of husbands to tell their wives whether or not they can have an abortion, Alito has upheld the right of police to strip-search a 10-year-old girl, and he has repeatedly thrown out cases charging racial discrimination and sexual harassment at work, on the grounds that workers needed to meet a standard of proof that legal experts say is impossibly high.<p>
"In one case [as a federal appeals court judge], his colleagues asserted that antidiscrimination statutes 'would be eviscerated if our analysis were to halt where [Alito's] dissent suggests,'" Seth Rosenthal recently reported in the <i>Nation. </i>"In another, he was the lone dissenter among 11 judges who voted to throw out a jury verdict favoring a worker alleging gender discrimination."<p>
Last month, it was revealed that Alito hasn't only scoffed at sexual harassment and racial discrimination lawsuits from the bench.<p>
In 1985, as Alito was applying to become deputy attorney general in the Reagan administration, he touted his membership in the "Concerned Alumni of Princeton"--a right-wing group formed in the early 1970s claiming that Princeton had lowered its admission standards to allow women and minorities entrance into the university.<p>
Even more disturbing are revelations about Alito's support of the government's right to spy on citizens. Last month, amid a new scandal over Bush's National Security Agency spying on U.S. residents without warrants, a memo that Alito wrote in 1984 showed exactly where his sympathies lie.<p>
In a case concerning a lawsuit against former Attorney General John Mitchell over a wiretap authorized without a court's permission, Alito--then a lawyer in the Reagan administration--argued that the attorney general and other top government officials should be "immune from violating clearly established legal standards" on the grounds of "national security." <p>
Alito also suggested that the Reagan administration shouldn't pursue the case involving Mitchell--because of its ties to the Nixon administration and the Watergate political scandal. Instead, he argued, "Our chances of persuading the court to accept an absolute immunity argument would probably be improved in a case involving a less controversial official and a less controversial era." <p>
Despite the mounting evidence of Alito's rotten record, the Democrats have shown few signs that they are planning to put up a real fight to stop his confirmation. While liberals like Sen. Ted Kennedy (D-Mass.) are asking for the release of more documents to look into Alito's record, party leaders are balking at even the mention of the word "filibuster."<p>
As Senate Minority Leader Harry Reid (D-Nev.) said on the Senate floor in November in response to charges that Democrats were considering a filibuster, "Democrats can hardly persist in an activity we are not engaged in. No Democrat has even raised the issue of extended debate." <p>
"As far as I can tell, the only person talking about a filibuster is Senator Frist and some of the far-right fringe groups," Reid's spokesman Jim Manley told the <i>Washington Post</i> last month.<p>

