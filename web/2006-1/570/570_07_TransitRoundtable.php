<font face="Times New Roman, Times, serif" size="4">TWU members speak out:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"We showed that striking <i>does</i> matter"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 6, 2006 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">HADAS THIER, SEAN PETTY and SARAH WOLF talked to transit workers about their reaction to the contract, the impact of the strike and the future of the labor movement.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="4"><b>Farrell</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><i>Train operator from the 147th Street yard</i></font><p>

<font face="Times New Roman, Times, serif" size="3">I CAN go home and sleep knowing I gave it to the man one time. I may be right or wrong, but I gave it to him. <p>
Economically, I don't think we accomplished anything. What would have been a victory? Walking away with 5-5-5 [percent raises] would have been a good number at a minimum. That's a fair amount--it's something that should have been a given.<p>
All the unions will benefit from the fact that we walked. This is nationwide. When we stepped down, we made this an issue for everyone. We hit Mr. Bloomberg and Mr. Pataki in their pocketbooks. Even if we come away with not winning the war, we won the battle. <p>
Those three days sent a shockwave through the city. This city showed its true colors--all the ugliness that was ever hidden was coming out. And if we had stayed out a few more days, it would have come out even more. <p>
How long do you lay down and allow it? We allow the lies. If you're going to allow people to step on you and not fight back, then you're going to get what you deserve. It's time that the little guy stepped up. <p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="4"><b>John</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><i>Train operator from the 207th Street yard</i></font><p>

<font face="Times New Roman, Times, serif" size="3">THE UNIONS that are negotiating need to come together as one. This is how government does it. It doesn't do things in bits and pieces--it works as a whole entity. <p>
That's how the labor movement should be. Everyone in the garment industry, whatever your title is, should all be in one union and all bargain together and shut down the whole industry. The same with transit. Metro North [commuter railroad] workers get more than we do. It should be that we all get the same thing. We should come together as one bloc, and then, when you shut it down, you shut it down. <p>
All of America is just getting tired of government. Government is putting a noose around people's neck. We're in a scary era. People are just tired of it--tired of Bush being arrogant and doing what he wants to do, the mayor doing what he wants to do. People all over America are just tired of it.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="4"><b>Ramon</b></font> <br>
<font face="Times New Roman, Times, serif" size="2"><i>Bus driver from the 133rd Street depot</i></font><p>

<font face="Times New Roman, Times, serif" size="3">I'M SKEPTICAL about the contact. In the last contract, as time went by, they added stuff. In the last contact, we had 12 sick days, but they told us to only take three sick days. I asked my boss, "When can I get sick?" <p>
Why does the Taylor Law exist? You shouldn't be able to restrict a union from striking. As long as you keep working, and they keep making their money, it's hard to get anything accomplished. Metro North has been working for over three years without a contract. Why is that? If the kids of this city are the future, why did the teachers go three years without a contract? The reason is that the city was cocky, and it had the Taylor Law behind them. <p>
The strike allowed other unions to say that this is how we need to stand up--that sometimes, we lose some to gain. What we showed is that it <i>does </i>matter when you strike. You have to fight for things. It's for the long run--to make things better. <p>

