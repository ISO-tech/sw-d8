<font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">May Day's massive protests for immigrant rights across the U.S.</font> <br>
<font face="Times New Roman, Times, serif" size="5"><b>A historic show of strength</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 5, 2006 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THE IMPACT of the massive demonstrations for immigrant rights held across the country on May Day--the international workers' holiday born in the U.S. 120 years ago out of the struggle of largely immigrant workers--will be felt for decades to come.<p>
No one can say for sure how many people marched and rallied on May 1--still less how many stayed away from work and school--but this was almost certainly the largest demonstration in U.S. history.<p>
A part of the population that has been consigned to the shadows of society came proudly alive on May Day. The theme for the national boycott--"A Day Without Immigrants"--highlighted their indispensable role in the U.S. economy and society, and in a way that even the mainstream media couldn't ignore.<p>
In hundreds of cities, in every state, from California to New York, Atlanta to Seattle, millions of people sent a message loud and clear: We're here, we want equal rights, and we will be heard.<p>
In a matter of weeks, this mass movement has grown to embody something far more than its immediate source--opposition to vicious anti-immigrant legislation passed last year by the House of Representatives.<p>
A huge gap remains between the aspirations of immigrant rights demonstrators and the proposals under consideration in Congress.<p>
The bill passed by the House, sponsored by Rep. James Sensenbrenner (R-Wis.), would classify undocumented workers as felons and add to the militarization of the border with an expansion of the wall along the U.S.-Mexico (though not the U.S.-Canada) border. But alternative legislation taking shape in the Senate, though it is called a compromise, would keep immigrants at the back of the bus.<p>
The latest deal--hammered out by Republican Sens. Chuck Hagel and Mel Martinez, but supported by liberal Democrats like Sen. Ted Kennedy--would divide undocumented immigrants into three tiers. Only those who can prove they've been in the U.S. for more than five years would be allowed to stay, and the supposed "path to citizenship" for even this group would be filled with punitive restrictions and barriers.<p>
The "compromise" also includes a guest-worker program pushed by Corporate America because it would help maintain a pool of low-wage immigrant labor with curtailed rights to protest their conditions. <p>
Democrats, along with some mainstream immigrant rights organization, insist that the Senate deal is the "realistic" alternative--and explicitly tied their support for it with opposition to the May 1 day of action.<p>
For example, Jaime Contreras, chair of the National Capital Immigration Coalition, said the boycott call for May 1 amounted to "throwing the atomic bomb...the last weapon in our arsenal." "We will carefully watch movement on the Hill and will reserve the tactic of a strike if and when it is most necessary," Contreras said.<p>
But masses of immigrant workers showed with their actions that they are ready to take a stand now--and not wait for what "the Hill" has to offer. "They'll be forced to think twice," said George Molina, who marched in San Francisco on May Day. "We'll be heard today. A closed mouth doesn't get fed."<p>
With another historic milestone past with May Day, the immigrant rights movement is turning to the challenges ahead.<p>
While claiming to be responding to the demands of demonstrators, the politicians in Washington are determined to force through immigration legislation that--under all the proposals being considered--would leave immigrants worse off. <p>
Our message is simple: No to Sensenbrenner, and no to any "compromise" that falls short of full equality and justice.<p>
In addition, this struggle will have to be taken to every workplace, school or community where immigrants are punished for participating in demonstrations. Unions and other labor organizations are organizing support for workers who were fired for taking off work to march.<p>
It will be crucial in these campaigns to win the unity of immigrant and native-born workers--with the argument that the fight of immigrants for their rights is a fight for all workers. Such struggles can build on the successes already seen in defending workers against victimization--and provide the starting point for activism that can help turn around the labor movement as a whole.<p>
Also, the racist Minutemen and the far right are lashing out at the growing immigrant rights movement. These bigots are a real danger, in the here and now--and they need to be confronted wherever they raise their head.<p>
These are big tasks and won't be accomplished overnight. But a struggle has been set in motion that won't disappear, no matter what the immediate course of the fight.<p>
The history of the U.S. working class is of long periods of seeming conservatism and passivity--shattered by eruptions of struggle that transform the political status quo and point the way toward a different world. We are living through one of those moments of struggle, and we need to make the most of it.<p>

