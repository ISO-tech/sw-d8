<font face="Times New Roman, Times, serif" size="4">Ronnie Kitchen and Stanley Howard:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"This is a fight that needs to be fought"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 5, 2006 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">RONNIE KITCHEN and STANLEY HOWARD are former Illinois death row prisoners and members of the Death Row 10--a group of African American men tortured into confessions by Chicago police. They told the <i>New Abolitionist</i>, a newsletter published by the <a href="http://www.nodeathpenalty.org" target="_blank">Campaign to End the Death Penalty</a>, why they support immigrant rights.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="4"><b>Ronnie Kitchen</b></font><br>
<font face="Times New Roman, Times, serif" size="3">I WISH I had a Mexican flag. I would be flying that in my cell. This is a civil rights issue and it's a human rights issue. There's no such thing as illegal. We're all immigrants. <p>
To see generations and generations of people fighting for what they believe in--that's a good thing. It's a fight that needs be to fought. <p>
The same people who are hollering that all illegals need to go back are forgetting that this country was built by immigrants--by immigrants and slaves! That's the sickest thing about it. Once upon a time, this country was inhabited by Native Americans. There were no whites--Europeans migrated over here. <p>
You have to support anybody who wants to better their lives. America's supposed to be the land of the free. What's wrong with people wanting to better themselves? We should support that. That's what America is supposed to be about. But the American Dream is a myth. <p>
They're fighting for rights to survive. I'm fighting for the right to survive. They want the same thing I want. They want to be able to walk the street without being arrested. They're fighting for education, housing, food, jobs. Me, I'm fighting for freedom. I'm fighting for my sons and their kids to have the same opportunities that rich white kids have. <p>

<font face="Times New Roman, Times, serif" size="4"><b>Stanley Howard</b></font><br>
<font face="Times New Roman, Times, serif" size="3">AS THOUGH the U.S. doesn't have enough problems at home and abroad, it has now turned its racist face on a so-called undocumented immigrant population. <p>
Immigrants have flooded into this country for over 400 years, including its so-called founding fathers, seeking a better life. It wasn't a problem then, or when over 12 million immigrants came here between 1892 and 1924. So why is it a problem now? <p>
With a highly prized congressional election coming up, this is nothing more than a well-orchestrated diversion, designed to take the attention away from the real problems and social issues that need to be addressed. Instead of turning our fellow brothers and sisters into criminals and undocumented felons or exploiting them as slaves for big corporations under a guest worker program, we should turn our attention to what's driving the exodus from their countries (i.e. the WTO, the IMF, NAFTA, CAFTA, etc). <p>
It's amazing how the indigenous people of this country were slaughtered and forced onto reservations by immigrants--and now the descendents of immigrants are turning on other immigrants, who came to this country for the same reasons their ancestors did. We must not allow them to scapegoat our brothers and sisters as political pawns. <p>

