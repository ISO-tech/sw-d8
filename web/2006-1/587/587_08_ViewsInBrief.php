<p><a name="SW"></a><font face="Times New Roman, Times, serif" size="4"><b>Reading <i>SW</i> behind bars</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I HAVE been receiving <i>Socialist Worker</i> while incarcerated in Martinez Detention Facility. My friend in the International Socialist Organization's San Francisco branch arranged the subscription for me while I was locked down.<p>
<i>SW</i> has been very useful in cementing a small group of anti-racist inmates together in a "Nazi-free" zone. After I read it, we circulate it through a readers' circle of six to eight men. It is greatly appreciated.<p>
I am writing to you to request that you transfer the remainder of the subscription to another prisoner. I will be getting out of jail on April 25, and I would like my inmate comrades to continue to receive <i>SW</i>. <br>
<b>Steve Orcutt</b>, Martinez Detention Facility, Martinez, Calif. <p>

