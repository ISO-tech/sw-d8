<font face="Times New Roman, Times, serif" size="4"><b>Dems abandon women's rights</b></font>

<font face="Times New Roman, Times, serif" size="3">LAST TIME I checked, the Democrats' strategy involved ignoring their base (especially women) and focusing on winning the votes of male veterans, NASCAR dads, Alabama guys with gun racks in their pickup trucks, and the "ban abortion" crowd. <p>
The uber-patriotic rhetoric, combined with support for the war, confirmation of right-wing judges, and distancing themselves from women's reproductive rights, is starting to make a lot of Democrats look like "Republican Lite": same bad aftertaste, one-third fewer votes. <p>
Repeat after me: It's not a winning strategy. Ignoring your base will not bring success at the polls. Ignoring the deeply held convictions of the majority of Democratic voters won't either.<p>
The Democrats want to convince us they have ideas that will turn this country in a new direction. I'm all for it. End this disastrous war, don't start a new one, restore public programs that were cruelly cut by this administration, raise the minimum wage and secure the homeland in a responsible way--not by cracking down on hardworking immigrants. And restore our right to privacy--on our telephones and in our bedrooms. Just for starters.<p>
But George W. Bush's losing strategy is not the Democrats' winning strategy. It will take more than "I'm not George Bush" to move the disillusioned to the polls. They will have to convince us that liberty and equality will have a place at the table.<p>
Speaking of George W. Bush's losing strategy, the Democracy Corps report had this to say about South Dakota's ban on abortions: "The South Dakota ban on all abortions, except for the life of the mother, is not a popular act. By 64 percent to 33 percent, voters say they would be less likely to support a candidate who endorsed it; almost half the country says they are 'much less likely' to support that candidate. That act, as it moves up the judicial ladder, may well create a new issue that pushes voters further away from Bush and the Republicans."<p>
Hmmmm...passing a law designed to challenge the Constitutional right to privacy that women have depended upon for more than three decades is unpopular? You don't say. <p>
This poll is good news because it sends a message to legislators on both sides of the aisle: Voters will not stand for lawmakers who try to control women's lives, and especially our reproductive health options. And both parties should remember that.<p>
Here's a thought: If people are moving away from the Republicans because of anti-women laws like South Dakota's, perhaps the Democrats should give them somewhere to move. They could start by ending the recruitment of Democratic candidates who would ban abortion, just like South Dakota. Now there's an idea.<p>
But November 7 is still six months away, and there's lots to do before then--like the April 29 "March for Peace, Justice and Democracy" in New York City. I'll be there, telling the world that I don't like George W. Bush's plan. And I think we need a new one.<br>
<b>Jon Wexler,</b> Atlanta<p>

