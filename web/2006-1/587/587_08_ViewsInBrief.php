<p><a name="NO"></a><font face="Times New Roman, Times, serif" size="4"><b>A world with no borders</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ROSEMARIE JACKOWSKI correctly observed in her letter, "The history of the southern border is relevant today." ("A call for open borders," April 7) However, the United States acquired far more than Texas as a result of the Mexican War, as it is called in U.S. history books. <p>
As part of what Sidney Lens describes as the "territorial fee" imposed on Mexico "for the privilege of being conquered," the United States acquired most of the Southwest, "an area greater than France and Germany combined." <p>
Jackowski may well be correct in identifying "the least we should do" as "allowing Mexicans to live and work here permanently, or else return Texas" and, I assume, the rest of the Southwest, to Mexico. Ultimately, however, the logic of "the new civil rights movement" cannot be contained within the boundaries of the present international political system. <p>
It demands the transformation of that system and the achievement of one in which the movement of people faces no more impediments than does that of capital in the current global system.<br>
<b>Mark Clinton,</b> Northampton, Mass.<p>

