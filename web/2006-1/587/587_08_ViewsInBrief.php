<p><a name="US"></a><font face="Times New Roman, Times, serif" size="4"><b>Immigration hurts U.S. workers</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WHY DOES your socialist organization defend illegal Mexican immigrants? These people are used to depress U.S. wages. How is it "pro-labor" to support this?<p>
It is my understanding that even Cesar Chavez was opposed to illegal immigration because the resulting surplus in labor only helped to further lower wages for the Mexican immigrant population.<p>
Why doesn't your organization better acknowledge that these are economic refugees from a Third World country under oppressive trade agreements arranged by the capitalists? The solution is not to admit these waves of refugees into the U.S. and thus increase racial tensions in this country. This only serves the interests of the capitalists. The solution would better be served by demanding that the U.S. desist from its harmful economic policies and begin to aid Mexico in its own economic development.<br>
<b>Jason Wright,</b> from the Internet<p>

