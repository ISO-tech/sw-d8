<p><a name="FTAA"></a><font face="Times New Roman, Times, serif" size="4"><b>Crushed by the FTAA</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CURRENTLY WE see the world in an uproar, with the immigrant issue. Many of us recognize that it is only a symptom of a greater threat. <p>
Could you please inform me as to where I can get more information on the Free Trade Area of the Americas (FTAA)? The word is out, if NAFTA hasn't crushed you, then the FTAA will. <p>
It is time to unite. We must get our voices back, and we must peacefully come into unity for the betterment of all citizens, pilgrims, or immigrants. We cannot continue to fight ourselves, and believe it is the "other guy's" problem. We have problems in this world, and it is up to each one of us, to seek a higher ground and a higher way of reasoning. <p>
It is too easy to be a member of the NRA and rally behind their reasoning. It takes civility, maturity, and wisdom to see the broader picture, and to make a commitment to get it done in a way that encourages and fosters permanent and lasting change for the betterment of all. <p>
Your reports and your information is important to me, it helps me to see another side of the media, and it is refreshing.<br>
<b>Elizabeth Estrada,</b> From the Internet<p>

