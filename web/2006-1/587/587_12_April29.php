<font face="Times New Roman, Times, serif" size="4">Could April 29 protest have accomplished more?</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Antiwar march takes to NYC streets</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Wengraf</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 5, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">AS MANY as 300,000 protesters jammed the streets of Manhattan April 29 for the first major antiwar demonstration in New York City in close to two years.<p>
The larger-than-expected turnout showed the spreading opposition to the disastrous U.S. occupation of Iraq. Thousands traveled from throughout the Northeast U.S. and beyond to attend the event, which was organized by the national antiwar coalition United for Peace and Justice (UFPJ), along with other liberal groups such as Operation Rainbow/PUSH, the National Organization for Women and the People's Hurricane Fund.<p>
A labor contingent of about 5,000 kicked off the day with words from Roger Toussaint, head of the New York City transit workers union, who was just released from prison, where he was sent by a judge for the union's determination not to accept the anti-union Taylor Laws during last December's transit strike.<p>
"A government that can't provide health benefits, pensions and education for its own has no business telling people around the world how they should live--has no business conducting an illegal war across the world," said Toussaint. "We need a working-class resistance." <p>
Student activists in the Campus Antiwar Network (CAN) came together for a spirited march contingent of hundreds of people. CAN provided a loud and visible presence on the march, with chants like "S&iacute;, se puede, we can stop the war" and "Hell no, we won't go, we won't kill for Texaco." <p>
Many people who turned out were driven by both anger at the ongoing occupation and the administration's new war threats against Iran. "The Bush administration has made the world a much more dangerous place," said Joan, who came with her daughter Laura from Ossining, N.Y. "We need more protest, like during the Vietnam War. It's about time to get started."<p>
Natalie from Harlem said, "I'm here to stand up for my beliefs--to stand up and make a difference. What hypocrisy it is from our government--in telling workers not to strike here on May 1, while they preach democracy in the Middle East. We're going to revolt against this."<p>
"We're in this war for no reason," added Jackie, who traveled from York, Pa. "Soliders are out there dying for nothing, and it has to be stopped."<p>
The huge turnout was a sign of the opportunity to revitalize and spread the national antiwar movement. Unfortunately, the organizers decided to limit the demonstration to a march that ended in an activists' fair.<p>
There were no speakers for the demonstration as a whole, though a few contingents organized speakers at their separate assembly points. The result was that there were no statements to give a sense of the stakes for the antiwar movement and its next steps.<p>
This format was in keeping with the increasingly moderate orientation of UFPJ and other leading voices in the antiwar movement. Their goal for April 29 was for the protest to be part of a broader mobilization behind the Democrats in the November election--despite the failure of the Democrats, including its liberal wing, to take an uncompromising antiwar stand.<p>
On the contrary, leading Democrats are continuing their attempt to pose as more competent on national security than the Republicans--for example, party leaders like Sens. Hillary Clinton and Barack Obama have tried to outflank the White House to the right in demanding action against Iran.<p>
Yet the April 29 demonstration largely focused on anti-Bush slogans rather than opposition to U.S. aims in Iraq and the Middle East, no matter which mainstream party is pursuing them.<p>
Ending the march with an activists' fair rather than a rally was designed to close down any likelihood that more radical politics--at odds with the message of the Democrats--would emerge.<p>
Veterans' and military families' groups had a large presence on the march, but this time around, their contingents were relegated far back from their usual spot at the lead--except for Cindy Sheehan, who marched behind the main banner with Rev. Al Sharpton, Rev. Jesse Jackson and Roger Toussaint.<p>
With no platform for speeches, these important antiwar voices went largely unheard. Jody Casey, a member of Iraq Veterans Against the War, was stationed in Baquba, Iraq, and left the military just last month. He traveled from Wenatchee, Wash., for the April 29 protest. "I've been dismissed as a 'disgruntled' soldier, but I would have been a lifer--I was a proud soldier," Casey said. "But we're tormenting innocent people in Iraq, people trying to lead ordinary lives, and the amount of destruction is outrageous." <p>
April 29 showed the potential for revitalizing the movement, but the real work of organizing will take place where it always has--at the grassroots.<p>

