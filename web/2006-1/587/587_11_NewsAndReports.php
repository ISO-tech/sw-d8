<font face="Arial, Helvetica, sans-serif" size="2">May 5, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Fight racism</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Matt Pillischer</b></font><p>

<font face="Times New Roman, Times, serif" size="3">PHILADELPHIA--A conference on "Poor, Young, Black and Male: A Case for National Action?" drew about 400 educators, students and community activists from across the U.S. to the University of Pennsylvania on April 20-21.<p>
Sociologist Elijah Anderson organized the conference to draw attention to and provoke action to deal with the crisis facing a growing number of young, Black, unemployed men, particularly in urban ghettos. "We need to start with the background of globalization, the interconnected global community," said Anderson. "And we also need to start with the legacy of white supremacy and slavery."<p>
The event brought together prominent figures such as Cornel West, William Julius Wilson, Douglass Massey and Michael Eric Dyson. Presentations and lectures covered a wide variety of topics and viewpoints on job discrimination, organizing in the pre- and post-civil rights eras, urban schools, Black men and the news, hip hop, sports, immigration, the gun market, law enforcement and the prison industrial complex.<p>
Cornel West called for action and denounced the "national inaction of the Black bourgeoisie itself." "You don't mention racism without mentioning the oppression of capital over labor, the bosses over the workers," he continued. "And here you're not just talking about Philadelphia, America, you're talking about New Delhi, Venezuela...You have to think globally."<p>
Massey made the case for why African Americans should support the explosion of the immigrant rights movement. "We need to organize together to fight against those in power who oppress, who work to subjugate both populations," said Massey.<p>
Gerald Jaynes, another speaker, added, "There will be a change in political currents because of the immigration movement...these are revolutionary times we live in."<p>
Many speakers, such as Michael Eric Dyson, connected the war on young Black men at home to imperialist wars abroad--and he viciously attacked the Republicans and Democrats for the witch-hunts against Arabs and Muslims. "Moussaoui plans to blow something up, and everyone with a turban is condemned," said Dyson. "Timothy McVeigh blows something up and everyone with a buzzcut keeps breezin' right through those metal detectors."<p>
Many people left the conference with the message that these are repressive times, but also potentially revolutionary times, and action needs to be taken on a grassroots level.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Defend civil liberties</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By John Osmand</b></font><p>

<font face="Times New Roman, Times, serif" size="3">OXNARD, Calif.--Around 75 people concerned about the curtailment of civil rights attended an April 8 statewide conference on gang injunctions at Oxnard College. <p>
Activists from throughout the state came to discuss the detrimental impact of repressive police measures in certain communities. In particular, civil gang injunctions (CGIs), such as the one in place in the neighborhood of Colonia, limit freedom of assembly, movement and expression.<p>
Conference attendees refute the notion that CGIs help reduce violent crime. In Colonia, there has been a decrease in property crimes since the CGI, but not other crimes such as homicide.<p>
City and county officials tend to impose CGIs on areas targeted for new economic development, resulting in accelerated gentrification of neighborhoods labeled a "war zone." Declaring such a zone amounts to collective punishment for the entire community, said several activists. One member of the organization Free West Sacramento was perplexed at the CGI in her community since no gang exists.<p>
The conference was organized by the Committee on Raza Rights and the Raza Press Association and was endorsed by several organizations, including the American Civil Liberties Union of Northern California.<p>
Conference participants concluded the daylong event by forming a statewide alliance called the Coalition for Youth, Justice and Human Rights. The coalition will be organizing a follow-up conference slated for July 22-24 in Sacramento.<p>
