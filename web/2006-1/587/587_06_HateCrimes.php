<font face="Times New Roman, Times, serif" size="5"><b>Far-right violence against immigrants</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 5, 2006 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">THE RACIST backlash against immigrants took a horrific turn last week when two white Texas teenagers sodomized and beat a Latino teen nearly to death--an assault that took place against a backdrop of escalating far right violence against immigrants.<p>
David Tuck and Keith Turner were attending a house party in the north Houston suburb of Spring on April 23. The two reportedly became angry after the 17-year-old Latino victim allegedly tried to kiss a 12-year-old girl, who they apparently believed to be white. <p>
According to witnesses, Tuck and Turner took the victim into the backyard and brutally beat him, while spewing racist slurs. The suspects then allegedly stripped the victim and sexually assaulted him with a piece of PVC pipe from a patio umbrella, causing massive internal injuries.<p>
Tuck and Turner also slashed the victim's chest while apparently attempting to carve a racist epithet into his skin with a knife, police said. Then, said investigators, the two suspects poured bleach all over the victim's body in an attempt to destroy any evidence. The victim has only a 50 percent chance of survival.<p>
Tuck reportedly sports Nazi tattoos and, according to KHOU television, at the time of the attack, he was on probation for his involvement in a cross-burning.<p>
The case brings to mind the 1955 lynching of Emmett Till--the 14-year-old Black teen who was murdered by racists in Mississippi for supposedly whistling at a white woman.<p>
Today, the far right is increasingly targeting Latinos. A recent report by the Anti-Defamation League (ADL) details how "white supremacists, neo-Nazis and other racists have declared 'open season' on immigrants" and lists a number of violent attacks against Latinos since 2002--including the January 2003 beating of a Latino man, also in Spring, Texas, by two men who hit and kicked him while shouting "border jumper," "spic," and "we kill people like you."<p>
This violent racism isn't just limited to organized white supremacist groups. It also is part of the so-called "mainstream" anti-immigrant groups like the Minuteman Project and the Border Guardians--which have organized vigilante "border patrols" in which they themselves detain supposed "illegals." <p>
While the Minutemen and similar groups claim to be anti-violence, the ADL report notes that several members of such organizations have been arrested on weapons charges. Meanwhile, according to a new report by the Southern Poverty Law Center (SPLC), Laine Lawless, the leader of the Border Guardians group in Arizona, recently sent an e-mail urging a leader of a neo-Nazi group, the National Socialist Movement, to violently harass undocumented workers.<p>
Anti-immigrant bigots must be opposed wherever they raise their heads--whether they march under the banner of far-right hate groups or claim to be mainstream.<p>

