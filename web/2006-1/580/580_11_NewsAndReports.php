<p><a name="Defend"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend immigrant rights</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WASHINGTON--Up to 20,000 supporters of immigrant rights rallied March 7 outside the Capitol. The rally, primarily organized by Capitol Area Immigrant Rights (CAIR) and Casa de Maryland, was called to protest the anti-immigrant HR 4437 also known as the Sensenbrenner Bill.<p>
The bill, which the House approved in December, would make undocumented immigrants felons, would bar U.S. citizenship to their children born in the U.S., and would criminalize social workers and others who help immigrants without papers by giving them food or shelter. <p>
The Minutemen, a virulent anti-immigrant group that supports the Sensenbrenner Bill, has recently moved into Maryland and is targeting day laborer centers run by Casa de Maryland, a pro-immigrant non-profit organization. Casa de Maryland was attacked by the national Minutemen organization after Casa de Maryland's executive director Gustavo Torres told a reporter that he would not be intimidated.<p>
"[W]e are going to picket their houses, and the schools of their kids, and go to their work," said Torres. "If they are going to do this to us, we are going to respond in the same way, to let people know their neighbors are extremists, that they are anti-immigrant."<p>
In response, the Minutemen launched a campaign to cut off Casa de Maryland's funding sources. Activists plan to build a committee of local organizations to protect immigrant rights and fight the Minutemen.<p>
-- In <b>Madison, Wis., </b>chanting "S&iacute;, se puede," some 40 protesters organized by Voces de la Frontera demonstrated against Wisconsin Assembly Bill 69, which would deny driver's licenses to undocumented workers and force current licenses to expire once a worker's visa expires.<p>
Leone, a member of the Service Employees International Union, told the crowd that this bill--along with the Sensenbrenner Bill--will have the effect of "making it more difficult to organize."<p>
"It will divide supporters by making them afraid to help us," said Leone. "They are going after the most vulnerable members of our society. If you're not an immigrant, they will come for you next!"<p>
<i>Nikita Pion-Klockner, Lauren Schmidt and Terry Schmidt contributed to this report.<p></i>

