<font face="Times New Roman, Times, serif" size="4"><b>Say no to war and occupation</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN DIEGO--Some 30 marchers arrived at Chicano Park in the Barrio Logan neighborhood on the afternoon of March 12 as part of a two-week, 241-mile march against war and injustice.<p>
The marchers began early in the morning 25 miles to the south in Tijuana, Mexico.<p>
Organized by Guerrero Azteca, the "Walk for Peace" (Peregrinaci&oacute;n por la paz) focuses on building antiwar activism in the Latino community and was initiated by war resisters Pablo Paredes, Camilo Mej&iacute;a and Aidan Delgado and Fernando Suarez del Solar, whose son, Jes&uacute;s was serving as a U.S. Marine when he was killed in Iraq on March 27, 2003. <p>
Along the way, the marchers will link up with local activists and make stops at important landmarks in the struggle against war and for Latino rights in the U.S. In San Diego, 30 peace and community activists met the marchers for a potluck dinner and rally in Chicano Park. <p>
In addition to Paredes and Suarez del Solar, immigrant rights activist Enrique Morones addressed the rally, linking the U.S. and Iraqi war victims with the deaths of immigrants crossing the border. From here, the walk continues to the Camp Pendleton Marine Base, then to Los Angeles and California's Central Valley, and arrives in San Francisco on March 27.<p>
<i>For more information, go to <a href="http://www.guerreroazteca.org" target="_blank">www.guerreroazteca.org</a></i>.<p>
-- In <b>Amherst, Mass., </b>more than 100 students, faculty and community members of the University of Massachusetts at Amherst marched on the Whitmore administration building to demand the university end its complicity in the war. <p>
The enthusiastic crowd led a procession through the halls to Chancellor John Lombardi's office to deliver more than 700 signatures in support of the UMass Antiwar Coalition's (AWC) demands, such as the exclusion of military recruiters from campus, the immediate release of the FBI agent employed by the university's police department and the refusal of all future research funding from the Defense Department.<p>
"I can confidently say, as a young working-class Latino, I have been more actively recruited by the military than by any university or any college," said student government candidate Elvis Menendez at the rally that began the march.<p>
In <b>Boston, </b>a protest of 40 people, organized by the Greater Boston Stop the Wars Coalition, came out March 11 to confront war criminal Henry Kissinger as he spoke at the John F. Kennedy Library on a panel entitled "Vietnam and the Presidency."<p>
Kissinger, who served as Richard Nixon's Secretary of State, is responsible for U.S.-sponsored war crimes and millions of deaths in Chile, Cambodia, Angola, Laos, Vietnam and East Timor. <p>
While Kissinger brazenly defended his genocidal policies, he could not avoid protesters who confronted him with chants of "Hey, Kissinger, you can't hide, we charge you with genocide!" <p>
<i>Richard Greenblatt, Mike Schwartz and Tony Udell contributed to this report</i>.<p>

