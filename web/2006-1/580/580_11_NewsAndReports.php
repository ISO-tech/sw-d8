<p><a name="Horo"></a><font face="Times New Roman, Times, serif" size="4"><b>Confronting David Horowitz</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Mark Clinton</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SPRINGFIELD, Mass.--About 20 activists confronted right-wing bigot David Horowitz during his March 9 speech at Western New England College. The activists energetically booed, hissed and heckled the almost mind-numbing multitude of racist, sexist, homophobic and misanthropic insults hurled by Horowitz--and quickly many more in the audience of 100 were joining in.<p>
Horowitz most clearly exposed his racism in describing the Palestinians. "Never in human history has there been such a sick society as the Palestinians," said Horowitz.<p>
Ironically, given his self-promotion as a crusader for free speech and ideological balance in higher education, Horowitz's appearance was carefully stage-managed to prevent anything so unseemly as a genuine political debate from breaking out.<p>
Uniformed and plainclothes security officers were present to prevent us from entering the auditorium with signs, search our bags prior to allowing us to enter and warn us that we could be removed if we were disruptive.<p>
The question-and-answer session that followed was limited to a handful of questions (we managed to ask four of the seven that were allowed) and was dominated by Horowitz's transparently evasive responses that nonetheless exposed his bigotries.<p>

