<font face="Times New Roman, Times, serif" size="5"><b>Occupation is source of Iraq's violence</b></font> <p>
<font face="Arial, Helvetica, sans-serif" size="2">March 17, 2006 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">PETER PACE doesn't want you to worry about how things are going in Iraq. "I wouldn't put a great big smiley face on it," the chair of the Joint Chiefs of Staff told NBC's <i>Meet the Press, </i>"but I'd say they're going well."<p>
Dick Cheney agrees. "Progress in Iraq has not come easily, but it has been steady," declared the hapless hunter of Republican lawyers. Donald Rumsfeld thinks that people would have a better attitude about the occupation if the media would just stop "exaggerating the situation."<p>
Actually, the only thing exaggerated is the increasing gap between the administration's fantastical propaganda and the reality on the ground in Iraq.<p>
Three years after the U.S.-led invasion, the crisis of Bush's occupation of Iraq seems to grow deeper with every passing week.<p>
According to the Pentagon's own optimistically named "Iraq Progress Report," issued to Congress three times a year, insurgent attacks against U.S. forces "reached a postwar high in the four months preceding January 20."<p>
The bombing of the Askariya mosque in Samarra in February unleashed a wave of sectarian violence, prompting even the Bush administration's fix-it man in Iraq, Ambassador Zalmay Khalilzad, to admit that a "Pandora's box" has been opened, with a full-blown civil war the potential outcome.<p>
Every lie that the Bush administration used to justify the invasion of Iraq has long since been exposed--from non-existent weapons of mass destruction to fabricated ties to al-Qaeda.<p>
The biggest lie of all was that the Iraqi people would welcome U.S. invaders as liberators. No one can possibly believe this today. A poll conducted by the Program on International Policy Attitudes and released at the end of January found that 70 percent of Iraqis wanted a timetable for U.S. withdrawal--and almost half approved of attacks on U.S.-led forces.<p>
There can be no denying this basic fact--anyone who says the U.S. must stay in Iraq is rejecting the clearly expressed wishes of the majority of the Iraqi people.<p>
That includes the supposedly antiwar Democrats who are for "responsible" withdrawal. They give their case the gloss of concern about the fate of Iraqis--insisting that immediate withdrawal would plunge the country deeper into sectarian and ethnic violence.<p>
This is a patronizing attitude straight out of the colonial past--that Iraqis are incapable of governing themselves, or even resisting the urge to kill each other, without the presence of a massively armed occupying force.<p>
In reality, the blame for the violence and deteriorating conditions in Iraq today belongs squarely with the U.S. government. Faced with opposition to the occupation among Iraqis, the U.S. has counted on a divide-and-conquer strategy to maintain its grip.<p>
Thus, it regularly collaborated with the Kurdish militia, known as the peshmerga, or the Shiite Badr Brigades in military and police operations--the very forces often accused of organizing death squads. The constitution and electoral system imposed on Iraq following the U.S. "handover" of power to a puppet government further reinforced these divisions.<p>
There has been resistance to Washington's scheming--based on a hatred of occupation that unites the vast majority of Iraqis. Thus, following the mosque bombing, there were joint anti-U.S. demonstrations of Sunnis and Shia in a number of Iraqi cities.<p>
But the conflicts stoked with such effort by the U.S. are having an effect. The only way to end them is for the force primarily responsible for pushing these divisions to the breaking point--the U.S. occupiers--to get out.<p>
Every day that the U.S. remains in Iraq is another day of violence, worsening conditions and the brutalizing humiliation of life under occupation--and another day closer to a possible civil war. That's why we say: U.S. out of Iraq now!<p>

