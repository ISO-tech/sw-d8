<font face="Times New Roman, Times, serif" size="5"><b>Politicians profit as we're left behind</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 17, 2006 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">HUNDREDS OF thousands of jobs lost. Household incomes plummeting. Schools shut down. You wouldn't know it from the local papers, from Gov. Rod Blagojevich or from the menagerie of Republicans that would like to unseat him, but Illinois workers are facing nothing short of a crisis. <p>
You will hear a lot about "corruption." Former Gov. George Ryan is on trial for racketeering and fraud in a scandal that helped drive Republicans from power for the first time since 1977. <p>
Scandal after scandal has been exposed in Mayor Daley's Chicago "machine": From kickbacks in the city's "hired truck" program to a possible heroin ring in the water department. The current governor--an airbrushed "machine" scion--is the subject of a corruption probe. Blagojevich's campaign spokesperson is a lobbyist who banked $120,000 from Morgan Stanley, which won a deal for a $765 million state bond issue in 2005.<p>
But these are just symptoms of the bigger scandal: Illinois workers are facing a crisis while the rich and powerful make out like bandits. If some politicians may (briefly) find themselves in the poky, that is just the price of maintaining the system's credibility while doing business in a state where business rules. <p>
Blagojevich would like to present himself as Illinois' answer to Bill Clinton: Pushing pro-business policies that don't forget those "left behind." <p>
Chicago's schools face a $328 million deficit, and 1,000 teachers have been laid off. Blagojevich is pledging to cover only one-quarter of the shortfall, but he has the gall to claim it as a victory in increasing education spending. Meanwhile, a $15 billion O'Hare Airport expansion is underway. There is plenty of cash for airlines and contractors, but little or nothing for airline and construction workers' children. <p>
When it comes to renovating Soldier Field or building Millennium Park, a $200 million monument to Mayor Daley's ego, the state and city coffers are flush. When it comes to public transportation or schools, the purse strings are tightened. In just two years, Illinois has raided state workers' pensions to the tune of $2.3 billion.<p>
Blagojevich's approval rating is less than 50 percent. With Democrats in charge and Republicans discredited, the state should be ripe for a left third-party challenge. But most of the Chicago's left was absorbed into the Democratic Party in the 1980s--and the state's small Green Party was severely wounded in the 2004 election. <p>
This vacuum has allowed Republican Jim Oberweis, who made anti-immigrant fear-mongering the center of his 2004 Senatorial bid, to pose as a "reformer" in the governor's race.<p>
We need more grassroots struggles--like those of teachers and community members fighting school closings. And it is time to build a left alternative to the bipartisan crime bosses that run this state.<br>
<b>Adam Turl, </b>Chicago<p>

