<font face="Times New Roman, Times, serif" size="5"><b>The drug industry's prescription for super-profits</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Susan Dwyer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 17, 2006 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Merrill Goozner, <b><i>The $800 Million Pill: The Truth Behind the Cost of New Drugs.</b></i> University of California Press, 2005, 297 pages, 16.95.<p></font>

<font face="Times New Roman, Times, serif" size="3">ACCORDING TO Families USA, a non-profit organization working on affordable health care, "From 1997 through 2001, national spending on prescription drugs rose nearly 20 percent each year; from 2000 to 2001 alone, spending rose 17.1 percent."<p>
When confronted with the high cost of prescription drugs, drug company spokespeople promptly reply that the high cost of developing new drugs has to be offset by high prices if Americans are to maintain the best health care system in the world.<p>
However arguable it might be that we have the "best health care system in the world," it's quite clear that some people profit enormously while others are hung out to dry. According to figures from the U.S. Census Bureau, the percentage of Americans without basic health insurance rose from 14.6 percent in 2001 to 15.2 percent in 2002.<p>
On the other hand, the pharmaceutical industry is the most profitable sector in the economy, with profits in 2000 totaling four times the average of Fortune 500 companies. Not including unexercised stock options, Pfizer chair William Steere Jr. took home $40,191,845.00.<p>
An excellent way to make sense of all these figures is Merrill Goozner's <i>The $800 Million Pill: The Truth Behind the Cost of New Drugs, </i>which exposes the myths that surround the development of new drugs, genetic research and biotechnology.<p>
In the first section of his book, Goozner looks at the rapid and lucrative rise of biotechnology firms; the second covers the story of the development of the AIDS cocktail; and the third section examines what are known as me-too drugs and patent protective drugs which form the basis for much of the industry's profit.<p>
Goozner's book makes it clear that there is no magic pill. The complexities of the human system require hundreds if not thousands of people working for years on the basic biology of a single protein or gene sequence before anything therapeutic can be achieved.<p>
Sudden leaps forward in understanding take place, but only because of the years of hard work, not in spite of them. It also requires millions of dollars in government funding to do the research, run animal studies and then clinical trials to bring a drug to market.<p>
Sometimes directed research works quickly, as it did in the development of protease inhibitors for AIDS patients, and sometimes it does not, as in the "cure" for cancer. In most cases, however, the drug companies get involved in research only when there's the promise of a profitable market.<p>
Goozner cites as a example Dr. Ellen Vitetta of the University of Texas Southwestern Medical Center who's been working on Hodgkin's lymphoma for 20 years and has several potential drug candidates near the trial stage: "When they begin to emerge, the drug companies begin sniffing around. Some of them want a license; some want to share in the trials; some want to sponsor the research and let us do the work; some want the rights if it ever reaches the finish line. My own view is I don't want them telling me what to do. I want to do this in a scientifically driven way."<p>
The scientifically driven way is not the way of Big Pharma, however, as shown in the book's last section. As an industry, like any other, it is driven by profit and the need to protect profit.<p>
Goozner shows that this has been true throughout the period of modern medicine. The big pharmaceutical companies spend millions developing drugs such as Vioxx and Celebrex that either are little or no improvement over generic drugs in long use, or are slight changes from patented drugs such as Prilosec which are about to loose their patent protection.<p>
When we learn that antacids create a $7 billion-per-year market, it becomes clear why the drug companies are so eager to protect patents and create copycat drugs to grab a share of the profit.<p>
The cost to the world's population in this drive for profits rather than curative medicine is becoming increasingly clear as the threat of avian flu becomes more pronounced. Further, diseases like tuberculosis--long thought to be a thing of the past--threaten large portions of the world. Those who read <i>The $800 Million Pill </i>will realize that Big Pharma is not equipped to solve any of these pressing problems.<p>
Nothing exposes the bankruptcy of capitalism quite like a life-threatening virus. Those of us who are not willing to trade lives for money need to redouble our efforts to work for a system that puts human need above any other consideration.<p>

