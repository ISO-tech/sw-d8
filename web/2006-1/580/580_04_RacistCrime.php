<font face="Times New Roman, Times, serif" size="4">Bigotry set the stage for a stabbing at Cornell</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Standing up to a racist crime</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 17, 2006 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">NEWS OF a racially motivated stabbing taking place February 18 at Cornell University stunned the campus community. <p>
The assailant, Nathan Poffenbarger, reportedly yelled racial comments before stabbing Black Union College student Charles Holiday in the shoulder, puncturing his lung and putting him in critical condition.<p>
While this hate crime comes as a distressing wake-up call to many at Cornell, the nature of recent race relations at Cornell has not been heading in any other direction. <p>
It was Cornell's administration that allowed the publication of the September 2005 issue of the far-right newspaper <i>The Cornell American</i>, which contained an article calling for students to arm themselves against "thuggish Black males." The campus has recently been bombarded with anti-Muslim articles in both the <i>American</i> and the other right-wing campus newspaper, <i>The Cornell Review</i>. <p>
The administration has taken no action against such blatant bigotry. In a message to the Cornell community, interim President Hunter Rawlings addressed the racial nature of the assault by ambiguously "[condemning] the crime and the circumstances that appear to have led up to it." <p>
So where is the opposition? Not from <i>Turn Left</i>, the liberal campus newspaper for which Poffenbarger was a writer. <p>
Editor-in-Chief Evan Marshak and Editor Emeritus Wayne Huang were quick to release a contrived statement arguing that "according to [<i>Turn Left</i>] editors and staff members who had worked with Nathan, he had shown absolutely no propensity for violence or racism" and that "<i>Turn Left</i> condemns any racist or violent actions." <p>
Clearly not from the Ithaca police, who reportedly told Holiday that he "can't get angry every time the N-word is used" at the crime scene and allowed Poffenbarger to wander away from the scene. And clearly not from the university administration. President Rawlings opted not to attend an emergency meeting on the incident in order to attend to houseguests.<p>
The opposition to such egregious bigotry is only viable from the bottom up. Despite a complete lack of response from the university, the Africana, LGBT, prison activism and <i>Black Perspectives</i> communities, among others, were quick to mobilize and demand answers. <p>
More than 100 students and faculty members gathered less than 48 hours after the incident to hear vague reports from the Cornell police, judicial administrator, and vice president of student life, who refused to even confirm that the stabbing was racially motivated. The angry and emotional crowd was sent away with little information. Holiday's friends, visiting from Union College, attended the meeting, but were silenced by the Cornell police. <p>
On February 27, however, 300 students came together to protest the university's longtime reluctance to provide equal opportunities to minority students, as part of a campaign to end violence and institutional racism at Cornell.<p>
In order to fight the onslaught of such bigotry on our campuses, we must organize on a grassroots level and regard administrations as obstacles rather than allies. <p>
Holiday is currently in good condition and is expected to be released from the hospital shortly. Poffenbarger is in custody and has been charged with assault. Cash bail is $25,000--only about half the cost of attending Cornell for a year, and pocket change for many students on the ruling-class campus.<br>
<b>Alan Ra,</b> Ithaca, N.Y.<p>

