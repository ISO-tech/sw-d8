<font face="Times New Roman, Times, serif" size="5"><b>Step up the effort to save South Central Farm</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 17, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES--More than 150 people rallied outside the March 10 city council meeting to demand action to save the South Central Farm, where 350 families supplement their low-wage incomes by cultivating 14 acres of land in the middle of LA's industrial sector. <p>
For three years, the South Central farmers have faced the threat of eviction from multimillionaire real-estate speculator Ralph Horowitz, who wants to raze the farm and build a warehouse. On March 1, county sheriffs posted an eviction notice at the farm.<p>
In the 1992 LA Rebellion, the racism and poverty experienced by the residents of South Central LA were revealed for the whole nation to see. As politicians scrambled to make token gestures of concern for the poor, the city permitted South Central residents to construct a community garden on some undeveloped land.<p>
A judge has ruled against Horowitz's efforts to lay claim to the land--three times! Then Horowitz cut a deal with LA's Democratic Party machine to buy the land for $5.1 million--about one-third of the property's estimated market value. <p>
The deal was made without a public hearing and without any communication with the South Central Farmers.<p>
"I will dedicate myself to transforming Los Angeles into the cleanest, greenest big city in America," said Antonio Villaraigosa during his campaign for mayor. But since taking office, Villaraigosa has taken no steps to protect the farm from Horowitz's bulldozers.<p>
The months-long campaign of rallies, phone calls and e-mails to pressure the mayor and the city has won more time to organize. That's the best way to save the South Central Farm from corporate greed and the two-faced politicians who feed it.<p>
