<font face="Times New Roman, Times, serif" size="4">Strikers enjoy huge show of solidarity in fourth week of strike</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Rally of 5,000 at Sikorsky</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jay Poppa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 17, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">STRATFORD, Conn.--In a tremendous show of solidarity, close to 5,000 strikers, members of other unions and supporters from around the East Coast rallied in front of Sikorsky Aircraft's main facility March 9.<p>
Some 3,600 members of Teamsters Local 1150, who work at Sikorsky in Connecticut and Florida, are entering their fourth week on strike.<p>
The strike began in response to Sikorsky's final contract offer that included a doubling of health care co-payments in the first year and 15 percent increases during the next two years, effectively nullifying any increase in wages or signing bonuses.<p>
"Several thousand brave men and women [are] fighting for a society where health care isn't only for the rich and where corporations are responsible members of the community," Teamsters president James P. Hoffa told the rally.<p>
The solidarity from other workers and organizations was the high point of the event. "It was inspiring to see that all of these people from other unions, other places, stand with us in our fight and see it as their own," said one worker. <p>
Connecticut United for Peace (CTUP), the statewide antiwar coalition, is one of the strike's ardent supporters and has issued a resolution supporting the strikers. "If we took the money we were spending on a war in Iraq and created a national health care program for everyone, then we wouldn't be on strike," said one worker in response to the support.<p>
However, the rally also exposed some of the strategic weaknesses of the Teamsters, both nationally and locally. While Hoffa is right to say that Sikorsky workers are part of a national fight for better health care, he is still unwilling to let the rank and file show its power. <p>
Instead, Hoffa and local union officials have put their faith in sympathetic politicians who have helped Sikorsky Aircraft secure a number of government contracts for helicopters. And local leaders haven't called for a full mobilization of workers to the picket line, but instead have organized only symbolic pickets.<p>
The union leaderships' strategy is to wait and see if Sikorsky comes back to negotiations instead of using its large membership to clog gate entrances to the Stratford facility. And because the local hasn't built a strike fund, workers could face serious financial difficulties if the strike goes on much longer.<p>
What's more, Hoffa has passed some of the largest dues increases in Teamsters history during his tenure as president, effectively doubling the union's budget, yet workers are still being required to pay dues while on strike.<p>
If there is one thing that the last three weeks has shown Sikorsky workers, it is that there is wide support for their struggle from people all over Connecticut and beyond. Workers and union officials should see this support as a green light to organize with their allies, put real pressure on Sikorsky and shut it down until their demands are met.<p>

