<font face="Times New Roman, Times, serif" size="5"><b>Free Iraqi women prisoners, free Jill Carroll</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 17, 2006 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">WHILE THE U.S. government and the media cynically try to use images of kidnapped reporter Jill Carroll to portray the resistance to the U.S. occupation of Iraq as fanatical, another conclusion is being drawn by antiwar activists who are actually concerned about winning Jill's freedom.<p>
Carroll, who has reported for the <i>Christian Science Monitor</i> on the horrible conditions endured by Iraqis under occupation<i>, </i>was kidnapped in Iraq on January 7. The stated demand of her kidnappers is the release of all women prisoners in Iraq.<p>
In January, U.S. military documents obtained by the Associated Press in a Freedom of Information Act request revealed that the U.S. Army has been arresting the wives of men they suspect of being a part of the Iraqi resistance in an efforts to get to the husbands. This practice has been denounced by Amnesty International and other human rights groups as tantamount to hostage taking, and therefore a violation of the Geneva Conventions.<p>
Unlike the U.S. government, which has done nothing to work for Carroll's release, antiwar activists are calling on both the U.S. to release the Iraqi women prisoners and for the kidnappers to release Jill Carroll.<p>
A petition, written by Palestinian activist Toufic Haddad, reads: "In light of the video aired on Al Jazeera satellite station on 30 January 2006 in which U.S. free-lance journalist Jill Carroll appeals for the release of all women prisoners from U.S. and Iraqi authority detention centers; In light of the recent revelation found in U.S. military documents that the U.S. Army in Iraq has jailed the wives of Iraqis suspected of being involved in the resistance, in the hopes of 'leveraging' their husbands to surrender; In light of the illegal and destructive nature of the U.S. occupation of Iraq, and the thousands of lives it has killed; In light of the billions of dollars of U.S tax-payer money that has been spent to finance this war, while vital educational, health, and social service programs are slashed at home; We the undersigned demand the U.S. government and Iraqi authorities unconditionally release all Iraqi women from its jails, and that Jill Carroll be immediately released, for the sake of her family and the families of Iraqi women prisoners"<p>
Supporters of four Christian Peacemaker Team members who were kidnapped in November and are still being held in Iraq are making a similar call. Their petition, which argues that the peace team's abduction is the result of the U.S. and Britain's occupation of Iraq, has been signed by prominent antiwar figures such as Arundhati Roy, Noam Chomsky, Cindy Sheehan, and Craig and Cindy Corrie.<p>
