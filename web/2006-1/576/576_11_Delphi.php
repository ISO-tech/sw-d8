<font face="Times New Roman, Times, serif" size="4">Judge hands executives $21 million in bonuses</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Pressure grows at Delphi</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 17, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">A BANKRUPTCY court judge approved tens of millions of dollars in bonuses for executives at Delphi Automotive just days before management was set to ask the judge to impose wage cuts on workers.<p>
Judge Robert Drain okayed the plan to pay $21 million to 400 Delphi executives for the first six months of 2006--including $5.7 million to the top 23 bosses. Drain did link payments to the auto parts company's achievement of financial goals, modifying Delphi CEO Steve Miller's plan to simply hand over cash as a reward for staying at the company during bankruptcy proceedings.<p>
Meanwhile, Miller wants the judge to cut the average $24 to $28 per hour wage nearly in half and dump pension payments onto the federal government.<p>
The aggressive move has finally pushed the leadership of the United Auto Workers (UAW) to instruct workers to prepare for a strike in case no agreement is reached before the judge imposes wage cuts, a process that could take weeks or even months.<p>
A strike at Delphi would quickly shut down its former parent company, General Motors, which still relies on Delphi for the majority of its parts. GM, which itself has extracted concessions on health care from the UAW in recent months, is itself at risk of bankruptcy in the event of a Delphi strike. The company lost $5 billion last year and plans to eliminate 30,000 jobs by 2008.<p>
Thus the UAW, Delphi and GM are negotiating to cover the costs of Delphi retirees to avoid a strike--but the big payout to Delphi executives has led many rank-and-file activists to conclude that a showdown is likely.<p>
"I think a lot of [UAW] people believed there would be a negotiated settlement between GM and UAW, and that it would be rational," said Gregg Shotwell, a worker at Delphi's plant in Coopersville, Mich., and an activist in the Soldiers of Solidarity network in the UAW that has been organizing a work-to-rule campaign.<p>
"Now, they see that the same people who caused the failure at Delphi are going to be rewarded at the same time we are getting wage cuts. It has become clear to people that the intent was to attack us, and that we would not be an easy settlement. Now, the bargaining committee chairs [at Delphi plants across the U.S.] are telling people to prepare for a long strike."<p>
The Soldiers of Solidarity network isn't leaving strike preparations to the UAW officials, who pushed through health care concessions at GM and Ford. "This is an underground movement," Shotwell said. "The people most involved in the work-to-rule campaign are not wearing buttons and distributing literature...It's the dog that doesn't bark that bites."<p>

