<font face="Times New Roman, Times, serif" size="5"><b>California judge calls for clemency</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Terzakis</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 17, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">CALIFORNIA DEATH row prisoner Michael Morales is scheduled to be executed at San Quentin State Prison at one minute after midnight on February 21--the third California prisoner to face an execution date in as many months. But as the California killing machine moves into high gear, it faces opposition from unusual places.<p>
Before the execution of Clarence Ray Allen in January, Dan Vasquez, a former warden at San Quentin, published an open letter to Gov. Arnold Schwarzenegger calling for clemency on the basis of Allen's advanced age and model behavior. Allen, Vasquez said, was no threat to society--and executing him would be "a shameful act."<p>
Then, at the end of January, Charles McGrath, the Ventura County Superior Court judge who presided over Morales' 1983 trial, wrote anther letter to Schwarzenegger, also requesting clemency.<p>
According to McGrath, the "aggravating circumstances" that precipitated Morales' death sentence came from a jailhouse informant, Bruce Samuelson, who was later found to be lying. "I believe," wrote McGrath, "that Mr. Samuelson's testimony was instrumental in convincing the jury, as it did me, that death was the only appropriate punishment in this case."<p>
Samuelson claimed that Morales had bragged in Spanish about the 1981 murder of Terri Winchell. But Morales, a fourth-generation American, doesn't speak Spanish. "Under such circumstances," McGrath continued, "executing Mr. Morales would frustrate the design of our sentencing laws, and would constitute a grievous and freakish injustice."<p>
Lawyer Kenneth Starr, now representing Morales in his appeals, agrees. "No enlightened society," Starr wrote, "should impose its ultimate sanction based on perjurious testimony."<p>
This is not the first time that California law enforcement officials have relied on questionable sources to secure a conviction and death sentence. Jailhouse informants--whose sentences were reduced in exchange for incriminating testimony--were also crucial to the conviction and sentencing of Stanley Tookie Williams, who was murdered by the state in December.<p>
And the use of jailhouse informants isn't the only freakish injustice in Morales' case: his co-defendant, Ricky Ortega was sentenced to life in prison for the same crime. Why the difference? Ortega was able to spend $80,000 on his defense, while less than $5,000 was spent on defending Morales--barely enough to cover legal fees.<p>
Morales' execution date is approaching amid a flurry of controversy over the state's preferred method of execution--lethal injection. The U.S. Supreme Court stopped an execution in Florida on the basis of defense attorneys' argument that lethal injection constitutes cruel and unusual punishment. Morales' attorneys are among those around the nation who have followed suit.<p>
As <i>Socialist Worker </i>went to press, anti-death penalty activists were awaiting a ruling on whether Morales' execution would be stayed to allow for a medical evaluation of execution procedures.<p>

