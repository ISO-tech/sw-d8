<font face="Times New Roman, Times, serif" size="4">Interviews with the people of Iraq and Afghanistan</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The face of war and occupation</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Glenn Allen</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 17, 2006 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Anne Nivat, <b><i>Wake of War: Encounters with the People of Iraq and Afghanistan. </b></i>Beacon Press, 2005, 320 pages, $25.95.</font><p>

<font face="Times New Roman, Times, serif" size="3">IN <i>THE Wake of War: Encounters with the People of Iraq and Afghanistan, </i>author Anne Nivat--unlike so-called embedded reporters--travels unescorted through Iraq and Afghanistan to find out what daily life is like for the people directly affected by the U.S. invasions and occupations.<p>
Part travelogue, part collection of oral histories, Nivat documents her travels by bus and taxi through these countries, dressed in a burqa or veil.<p>
Her goal was to tell the stories of those "from under the ruins"--those who live in the countries under U.S. occupation. She does this by combining history and recent events with interviews.<p>
The interviews are extensive and frequently intimate. They include members of the major and minor ethnic and religious groups in Iraq and Afghanistan. She speaks with engineers, traders, interpreters, former Taliban and new political leaders, with a Dominican friar who spends his days in one of the new Internet cafes, a former admiral in the Iraqi navy who still haunts his old base and General Rashid Dostum, a major Afghani warlord and member of the "Northern Alliance."<p>
We gain a sense of the complex interactions and strife between the different ethnic groups in both countries. Her travels start in Kurdish Iraq where people are dealing with the aftermath of the Baath party's "Arabization" program. This is followed by a series of interviews with ethnic Turks, who feel primarily oppressed by nearby Kurds. She recounts similar instances of ethnic resentment as she travels the various regions of Afghanistan.<p>
Although it's clear that Nivat sympathizes with the resistance and hates the U.S. occupation, she rarely editorializes, allowing her subjects to speak for themselves. The result is complex and frequently contradictory.<p>
A resistance leader brags that his group includes Sunnis, Shiites, Christians, Kurds and Turks, while many Kurds and Muslims express the opposite views. The widespread looting that followed the initial U.S. invasion of Iraq is variously blamed upon Kurds, Kuwaitis and Arabs.<p>
One thread that runs through the book is a hatred of the occupation, both for the major cruelties and the small stupidities of the Americans, which are often detailed in her conversations. She is invited into many homes and becomes friends with many of her interviewees. This gives us an intimate look into the inner lives of many of these characters.<p>
One of her guides talks about the extreme stress that the lack of a male heir brings to his family. An Iraqi teenager longs to travel in the West so that he can dress as he likes, and possibly have a sexual encounter.<p>
In Afghanistan, she spends a great deal of time analyzing women's situation. She interviews one of the two female representatives at the 2001 loya jirga, as well as a gynecologist who has never touched the hand of a man.<p>
This is combined with small observations about everyday life that are absent from other reporting. For example, we learn that the burqa is seldom worn in rural areas, since all males in the area would be relatives. Faces are quickly covered when buses approach, however.<p>
Nivat succeeds in humanizing those she meets, even the most brutal. This includes a short interview with a 26-year-old former member of the Saddam Hussein's Saffa Brigade, whose job it was to murder or kidnap for "reasons of state." He counts the number of tongues and hands he cut off and is only now beginning to come to terms with his past. The interview takes place at a caf&eacute; because he does not want to expose his family to the brutal stories, who he is now trying to put before all else.<p>
<i>The Wake of War's </i>biggest weakness is Nivat's lack of Arabic or Farsi, so she primarily interviews those who speak English, French or Russian, who are more well-educated and more prosperous than the general population. Despite this, you come away from <i>The Wake of War</i> with a sense of the humanity and difficulties faced by those under U.S. occupation, as well as the complex interactions between ethnic groups.<p>

