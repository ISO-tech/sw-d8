<font face="Times New Roman, Times, serif" size="4">Bush budget calls for deep cuts...except for the Pentagon</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Let them eat guns</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 17, 2006 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">GEORGE W. BUSH unveiled his 2007 budget last week with talk about a "brighter economic future for all Americans" and winning the "war on terror."<p>
But it didn't take much examination to see that the administration's proposal will make draconian spending cuts in programs that workers and the poor rely on--while pumping up the defense budget and handing the super-rich a massive tax cut.<p>
"Bush's budget wants us to declare that we should keep channeling ever more of the nation's wealth to those who are already the most wealthy, and to fund it by taking money from education, the poor, the disabled and from the impoverished elderly," Robert Freeman wrote on the Common Dreams Web site. "That is the naked meaning of his never-ending tax cuts for the wealthy and his program cuts for the weak."<p>
All in all, some 141 domestic spending programs face sharp reductions or outright elimination. Among the programs that Bush's budget would terminate altogether are:<p>
-- A program that provides food packages to more than 420,000 low-income elderly people;<p>
-- A preventive health care program to provide services to populations without access to health care,<p>
-- A program run largely through historically Black universities to help minority students finish high school and go to college.<p>
The administration budget proposal would cut $304 million from the Environmental Protection Agency and $2.1 billion from education spending.<p>
During the next five years, $10.1 billion in spending on veterans' services would be eliminated--even as the number of veterans returning from U.S. wars with a host of physical and psychological ailments surges.<p>
But while Bush wants to cut spending on the soldiers who fight his wars, he's ready to dramatically increase spending on their weapons. Military spending is slated for a 6.9 percent increase--a total of $28.4 billion--for a total of $439.3 billion.<p>
The increase for the Pentagon dwarfs the $14.5 billion that would be eliminated from the 141 social programs on the chopping block.<p>
And this doesn't include the costs of fighting wars in Iraq and Afghanistan. Bush said that he'll ask Congress for $120 billion for this--compared to $18 billion for hurricane relief.<p>
At the same time, Bush wants to make his tax cuts for the rich a permanent part of the tax code--at a cost of $1.7 trillion over 10 years.<p>
When these tax cuts come fully into effect, the average tax cut for people in the middle of the income scale would be $650. Taxpayers whose income is more than $1 million a year would get a $136,000 tax benefit, according to the Tax Policy Center, a joint venture of the Urban Institute and Brookings Institution.<p>
During the next 10 years, the richest 1 percent of the U.S. population will get a total tax break of $1 <i>trillion</i>--nearly 60 percent of the total tax cut.<p>
Just a fraction of this giveaway to the super-rich could pay for the deep cuts that Bush has planned for entitlement programs. The Medicare health program for the elderly and disabled faces cuts of $35.9 billion over five years. The Medicaid program for the poor would lose $17.2 billion.<p>
And while big corporations like United are abandoning their pension obligations by dumping them into the federally guaranteed Pension Benefit Guaranty Corp. (PBGC), Bush wants to cut $16.7 billion from the PBGC budget--exposing millions of workers to even steeper losses in their retirement benefits.<p>
Bush's "let them eat guns" budget ran into problems even among Republicans. Sen. Arlen Specter (R-Pa.) called the proposed cuts to health care and education "scandalous," and Sen. Olympia Snowe (R-Maine) said she was "disappointed and even surprised" by cuts to Medicare, Medicaid and veterans' benefits.<p>
Divisions within the Republicans are growing--between those like Snowe who worry about selling a budget that so nakedly punishes the poor while rewarding the rich, and conservatives who are furious that the administration is responsible for a new record budget deficit of $423 billion this year. "I hope we don't have to get back in the minority to discover our roots," said Rep. Jeff Flake (R-Ariz.).<p>
And the response of the "opposition" Democrats? Party leaders seemed content to echo the rhetoric of the GOP <i>conservatives</i>, complaining that the Bush proposal "lacks fiscal responsibility." "This budget is utterly detached from any financial reality," lectured Sen. Kent Conrad (D-N.D.), the ranking Democrat on the Senate Budget Committee.<p>
But in early February--just days after passing $40 billion in emergency budget cuts that targeted Medicaid, Medicare, welfare, child support and student loans--Senate Democrats joined Republicans to pass $70 billion in tax cuts for corporations.<p>
Actually, the Democrats are "detached from the financial reality" faced by workers, students, the poor and the elderly in the U.S. today.<p>
Bush's popularity ratings are dragging along at all-time lows for his presidency. The Democrats should be on the offensive, and the White House should be in retreat mode.<p>
But the Bush administration has always responded to any crisis by forging ahead and trying to steamroller any opposition. We can't expect any part of the Washington political establishment to stand up to Bush's scorched-earth policies.<p>

