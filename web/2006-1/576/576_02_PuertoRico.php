<font face="Times New Roman, Times, serif" size="5"><b>FBI raids homes in Puerto Rico</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By H&eacute;ctor Reyes</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 17, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">JUST FOUR months after the FBI assassinated Puerto Rican independence activist Filiberto Ojeda R&iacute;os, agents were back persecuting other independence activists. On February 10, the Feds raided and ransacked five homes and the office of a faith-based community organization active around affordable housing issues.<p>
FBI officials claimed the raids helped thwart a potential "domestic terrorist attack." Dozens of agents--many from a special squad based in Miami--confiscated files, computers, fax machines and cell phones. While no one was arrested, several people were detained and kept handcuffed for hours.<p>
In the R&iacute;o Piedras sector of the capital, the FBI was confronted by independence supporters, students and socialists. Agents proceeded to push back the crowd and used pepper spray indiscriminately--even directly on the faces of journalists covering the confrontation.<p>
Yet in a public statement, the FBI had the nerve to say that the crowd was pepper-sprayed "to protect members of the media."<p>
The people targeted by the FBI belonged to Rompiendo el Per&iacute;metro (REP, Breaking the Perimeter), a new coalition formed after Ojeda R&iacute;os' murder. The REP has organized public protests across the island demanding a full investigation into the death of R&iacute;os, who was shot and left to bleed to death by the FBI.<p>
The FBI's actions this month were so offensive that even Puerto Rico's Gov. An&iacute;bal Acevedo Vil&aacute; was forced to distance himself, claiming no prior knowledge and declaring that "there is no justification for the use of excessive force."<p>
The FBI claims that it was attempting to pre-empt a terrorist attack by the defunct Boricua People's Army--known as the Macheteros--which Ojeda R&iacute;os was a leader of, and which was accused of a $7 million Wells Fargo heist in Connecticut in 1983.<p>
According to the FBI's press statement, the alleged attack by the Macheteros, "where explosive devices were to be utilized, was directed at privately owned interests in Puerto Rico, as well as the general public." But this is more of the same manipulation that the Bush administration has used to justify its atrocities and the trampling of rights at home and abroad.<p>
As former Puerto Rican Independentist Party legislator David Noriega said in a San Juan radio interview, "The only logical explanation for this atrocity is that there is some kind of cruelty, an anti-Puerto Rican feeling, because after all they are unleashing their anger...against a people that kicked them out of Vieques."<p>

