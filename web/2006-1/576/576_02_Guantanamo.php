<font face="Times New Roman, Times, serif" size="5"><b>Tortured for the "war on terror"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 17, 2006 | Pages 1 and 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE BUSH administration claims that prisoners of the U.S. "war on terror" are treated humanely. But sickening new revelations from the U.S. prison camp in Guant&aacute;namo Bay, Cuba, reveal the truth.<p>
Dozens, and sometimes hundreds, of the approximately 500 detainees at the prison camp have been on hunger strike at various times over the past several years, in protest against U.S. policies of indefinite detention and physical and psychological abuse from guards.<p>
According to the <i>New York Times</i>, U.S. guards--fearing that prisoners engaging in the most recent hunger strike would succeed in committing suicide through starvation--recently begun strapping detainees into "restraint chairs" for force feedings. The detainees have been left tied in an upright position for hours at a time to prevent them from vomiting food.<p>
Riot-control soldiers violently force long plastic tubes down the noses and into the stomachs of hunger strikers. Prisoners also say that guards have subjected them to extended periods of isolation, exposure to cold air and stripped them of "comfort items" like blankets and books in order to force them to end the hunger strike.<p>
Thomas Wilner, a lawyer representing six Kuwaiti detainees, told the <i>Times</i> his client, Fawzi al-Odah, said in December that guards began confiscating shoes, towels and blankets from hunger strikers.<p>
Odah said he heard "screams of pain" from a hunger striker in the next cell as a thick tube was inserted into his nose. He also said that guards mixed laxatives into the liquid formula they force-fed to about 40 prisoners, causing them to defecate on themselves.<p>
Lawyer Joshua Colangelo-Bryan told the <i>Times</i> that his client, Jum'ah al-Dossari, said more than half of a group of 34 long-term hunger strikers had been forced to abandon their protest after being strapped in restraint chairs and force-fed through the plastic tubes. The tubes were inserted and removed so violently that some prisoners bled or fainted, Dossari told his lawyer.<p>
"He said that during these force feedings, too much food was given deliberately, which caused diarrhea, and in some cases caused detainees to defecate on themselves," said Colangelo-Bryan. "Jum'ah understands that officers told the hunger strikers that if they challenged the United States, the United States would challenge them back using these tactics."<p>
U.S. military officials claim that this treatment is humane. "There is a moral question," Assistant Secretary of Defense for Health Affairs, Dr. William Winkenwerder Jr., told the <i>New York Times</i>. "Do you allow a person to commit suicide? Or do you take steps to protect their health and preserve their life?"<p>
But as an 18-month investigation by the United Nations Commission on Human Rights recently concluded, violent force-feeding of hunger strikers, incidents of excessive violence and interrogation techniques at Guant&aacute;namo "must be assessed as amounting to torture." "Protecting" health and "preserving" lives is the last thing on the Bush administration's mind.<p>
Lt. Col. Jeremy Martin, the chief military spokesman at Guant&aacute;namo, recently declared to the <i>Times</i> that "hunger striking is an al-Qaeda tactic used to elicit media attention and also to bring pressure on the U.S. government."<p>
But according to a new analysis of declassified Defense Department evaluations carried out by Seton Hall Law School professor Mark Denbeaux and attorney Joshua Denbeaux, 92 percent of the prisoners at Guant&aacute;namo aren't even classified as al-Qaeda fighters. And just over half of detainees at the prison camp are even accused of committing a "hostile" act against the U.S.--even though a "hostile" act is defined, in some instances, as nothing more serious than possessing rifles, using a guest house or wearing olive drab clothing.<p>
"Now, for the first time, the military's lies and misrepresentations about the prisoners in Guant&aacute;namo have been debunked through the military's own documents," Center for Constitutional Rights attorney Gita Gutierrez said in a statement. "Yet these men remain in prison, while at every turn, the [Bush administration] seeks to avoid judicial scrutiny of its unlawful conduct."<p>

