<font face="Times New Roman, Times, serif" size="5"><b>Bush knew...and did nothing</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 17, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE BUSH White House knew within hours that the New Orleans levee system had been breached by Hurricane Katrina--just as federal disaster officials had predicted.<p>
But in that crucial first 24-hour period, as floodwaters inundated New Orleans and the nightmare began for the poor and mainly Black residents who couldn't evacuate, administration officials--from Bush on down--kept insisting that all was well. And did nothing to stop a natural disaster from becoming a human catastrophe.<p>
In the aftermath of Katrina, administration officials claimed that they didn't know the levees were breached until more than a day after the storm struck. But evidence has emerged to expose this lie.<p>
After Katrina hit the Gulf Coast on August 29, there was one Federal Emergency Management Agency (FEMA) official, Marty Bahamonde, on the ground in New Orleans. According to congressional investigators, Bahamonde heard about a major levee breach that day, viewed the damage himself by helicopter before nightfall--and immediately notified the Homeland Security Department and the White House.<p>
Conditions, an e-mail from Bahamonde reads, "are far more serious than media reports are currently reflecting. Finding extensive flooding and more stranded people than they had thought--also a number of fires."<p>
But for much of the next day, the administration claimed that New Orleans had "dodged a bullet," as Bush put it, speaking from his vacation ranch.<p>
In Senate testimony last week, Michael Brown--the Bush crony put in charge of FEMA who became the administration's all-purpose scapegoat after he was forced to resign--confirmed that the White House knew exactly what was taking place in New Orleans, and much earlier than anyone knew.<p>
Clearly angry at being hung out to dry by his former pals, Brown is now admitting what he wouldn't as a loyal member of the administration--that FEMA was gutted when it was turned into a division of the terrorism-obsessed Department of Homeland Security, and unprepared to act.<p>
"It is my belief," Brown told the committee, that if "we'd confirmed that a terrorist had blown up the 17th Street Canal levee, then everybody would have jumped all over that and been trying to do everything they could."<p>
The hammering of the White House continued this week with a report slated to be issued by a House select committee. The report puts the blame on incompetence at all levels of government, but the Bush administration--especially Homeland Security Department Secretary Michael Chertoff--gets most of the attention.<p>
For example, Congressional investigators have discovered that the Feds failed to order any food and water supplies for the New Orleans Convention Center until days after the city opened it as a backup emergency shelter.<p>
What's more, the day before Katrina hit, Homeland Security officials predicted that the levees could breach and submerge the city, leaving 100,000 people stranded. But no coordinated effort was made to evacuate city residents without cars, or even patients in nursing homes and hospitals.<p>
Investigators "are left scratching our heads at the range of clumsiness and ineptitude that characterized government behavior before and after this storm," the report concludes, according to the <i>Washington Post</i>. "If this is what happens when we have advance warning, we shudder to imagine the consequences when we do not."<p>
What makes the critique even more notable is that it comes from a House committee composed <i>entirely </i>of Republicans--Democratic members had boycotted the inquiry, expecting that it would result in a cover-up.<p>
Meanwhile, even as the White House came under fire for its criminal negligence at the beginning of the disaster, FEMA was set to enforce a deadline--delayed several times before--for cutting off evacuees from vouchers paying for hotel rooms as temporary shelter.<p>
As many as 12,000 families left homeless by Katrina are to be scratched off the rolls now, according to the <i>Times-Picayune--</i>with another 8,000 to follow by the start of next month. Many of the families who will be forced out are waiting on temporary trailers promised by FEMA--which have yet to arrive in most cases.<p>
As Daniel Greenberg, a lawyer who helped in a lawsuit that forced FEMA to delay earlier plans to scale back the hotel room program, put it, "This is just one more of the tragedies that happen when FEMA is more concerned about its pocketbook and fraud than it is for evacuees."<p>

