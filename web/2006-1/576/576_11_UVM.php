<font face="Times New Roman, Times, serif" size="5"><b>UVM plans to lay off teachers spark outrage</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nancy Welch, United Academics-AFT/AAUP, delegate</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 17, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">BURLINGTON, Vt.--Education major Tricia Preston had tears in her eyes as she delivered a message to University of Vermont (UVM) President Daniel Mark Fogel.<p>
"You can talk about numbers all you want, but that's not why I'm here," said Preston, walking up to the president at the February 10 Board of Trustees meeting and handing him a petition signed by 1,500 UVM students.<p>
She turned and pointed to six women standing together, with nearly 100 other faculty and students, at the back of the room. "Those teachers are why I'm here," Preston told the president and trustees. "If you let them go, you will crush this program."<p>
Preston, a junior, is a member of the newly formed Education Majors for Justice. With the Student Labor Action Project and the faculty union, they are protesting the layoffs of six long-time lecturers in the Department of Education plus university plans to terminate up to 22 more. All six of the lecturers receiving pink slips so far are women between the ages of 50 and 60 who have worked at UVM for eight to 20 years.<p>
At a candlelight vigil on the eve of the trustees' meeting, one veteran lecturer explained that she's being fired just months before she would have been eligible for retirement benefits. "Shame! Shame!" responded the crowd of 280 faculty and students who turned out for the event despite subzero temperatures and blowing snow.<p>
The two events marked the largest mobilization of UVM students in more than a decade.<p>
University administrators claim the layoffs are needed to balance the department's budget. However, these same administrators have asked trustees to approve $15.4 million in new construction. President Fogel also crowed of a record high in new student applications.<p>
"Classes, not construction!" and "Good teachers don't grow on trees!" proclaimed the signs held by students who collected the 1,500 petition signatures in only five days. Students, parents and alumnae have also flooded the president's office with e-mails and letters.<p>
The Department of Education, with 450 undergraduate majors, isn't "over budget" but "underfunded," argued faculty union president David Shiman to the trustees. If the layoffs proceed, he said, the secondary education program will be left with only one and a half faculty members.<p>
Handing President Fogel a candle from the previous night's vigil, Shiman added, "We hope this will light your way to a better path."<p>
With top administrators still insisting on cuts, faculty and students plan to step up the protest calls to the president as well as launch a community petition. Students are also considering taking their message to prospective students at the upcoming admitted student visitation days.<p>

