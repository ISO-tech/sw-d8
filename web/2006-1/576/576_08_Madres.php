<font face="Times New Roman, Times, serif" size="5"><b>Why did the Madres disband?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 17, 2006 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">AFTER 25 years and 1,500 consecutive Thursdays, the Madres de Plaza de Mayo ended their historic weekly demonstrations on January 26.<p>
The Madres are the organization of the mothers of those "disappeared" in Argentina by the military dictatorship of 1976-1983. In those years, people were targeted for the slightest sign of dissidence. Over 30,000 were kidnapped by the state, never to be seen or heard from again.<p>
With the military still in power, in the face of brutal repression and state terror, the Madres gathered the courage to protest. Holding up photos of their children and signs that read, "Where are they?" they began to march every Thursday around the monument of Plaza de Mayo, in front of the Presidential Palace in Buenos Aires.<p>
The increasingly left-wing organization led by Hebe de Bonafini did not dismount their protest when "democracy" returned in 1983, because they recognized that the dictatorship had been part of a larger imperialist socio-economic system that continued to bring misery to Latin America.<p>
The Madres have long been the brightest beacon of courage, perseverance and inspiration for anyone fighting for change in Argentina.<p>
This is why it is with great pain and disappointment that many of us listened to Hebe close the last "March of Resistance" with the justification that they are no longer necessary, because "there is no longer an enemy in the government." "Today, the president [Nestor Kirchner] is a friend of the Madres," Hebe explained.<p>
The problem is that Kirchner is in the Peronist party; the party that handed power to the military in 1976, the party of Carlos Menem, who presided over the most complete neo-liberal plunder of Argentina during his presidency (1989-1999).<p>
While he has repealed the laws that gave official impunity to the criminals of the dictatorship (one of the Madres' main demands) and his overall rhetoric leans left, Kirchner is still but the overseer of the same capitalist system that continues hyper-exploiting Argentina's people and resources in the interests of imperialism and the national elite.<p>
He recently paid off Argentina's entire debt to the International Monetary Fund. Who knows how many lives the $9.8 billion he "disappeared" that are direly needed in the slums and hospitals have cost us!<p>
The capitulation of the Madres, who until 3 years ago proclaimed at demonstrations their convincement that "the socialist revolution is the only way out," to Kirchnerism is a sign of how deep the center-left governments of Latin America are capable of reaching to absorb sections of the left.<p>
However, in this period of mass radicalization in this continent, there isn't a lack of people to step up as the Madres once did alone.<p>
The decision to cancel the "Marches of Resistance" was met with a chorus of disapproval by broad swaths of the left, including Estela Carloto, president of the Abuelas de Plaza de Mayo--a different section of the Madres whose principal struggle has been the tracking down of children of the "disappeared," born in captivity and often adopted by military families.<p>
"We must keep fighting," Carloto declared, "not all the assassins are in prison and we still haven't found 500 stolen children."<br>
<b>Federico Moreno, </b>Argentina<p>

