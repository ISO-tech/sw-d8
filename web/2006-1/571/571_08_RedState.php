<font face="Times New Roman, Times, serif" size="5"><b>The truth about a red state</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 13, 2006 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">THE ELECTION season is in full swing in Arizona. A flurry of poll results has splashed across the front pages recently, presenting a fascinating picture of what ordinary people are thinking right now in this, among the reddest of the "red states."<p>
On health care, fully 81 percent support universal health care. Chris Philbin, a 26-year-old registered Republican who currently has health coverage, stated in a focus group follow-up: "I would gladly give up 10 percent of my paycheck to have free health care like they do in Canada--free prescriptions, everything." (Interestingly, Canadians give up far less that 10 percent of their paycheck to get just that.)<p>
This overwhelming support for universal health care should come as no surprise. Some 20 percent of Arizona's 5 million residents have no health coverage at all, another 20 percent are on the state's Medicaid program, and a good portion of both camps works for one of the state's largest private employers: Wal-Mart.<p>
Health care isn't the only issue burning here, though. Not one, but two antigay coalitions have been pushing for a state constitutional ban on same-sex marriage. <p>
Recent polls confirm that over half of Arizonans are against a ban on same-sex marriage, even if they aren't for gay marriage itself. While it is common knowledge that younger people are more supportive of same-sex marriage, 62-year-old Manuel Varela sheds some light on those who are against it. "I would ban same-sex marriage, but I think that those partners should receive benefits," he said. "Too many people go without benefits, health insurance especially."<p>
The very "un-red" support continues: 76 percent support a minimum-wage hike to $6.75 an hour, and, in the state that is second to last in terms of per-pupil spending in education, 72 percent want the state to spend more, including some 64 percent who support universal, all-day kindergarten.<p>
Two things make this widespread support for such basic, progressive ideas critical. One is the necessary reminder that even in supposed conservative backwaters like Arizona, the gap between the right-wing lunatics in the state house (and in Arizona, they really are lunatics) and what ordinary people think is enormous.<p>
The other is that Janet Napolitano, the current Democrat governor, is up for re-election. She is a superstar in the party now because she has figured out how Democrats can govern in a "red state." But she has done that not by tapping into this massive base for progressive programs, but instead by catering to the right. <p>
The numbers in these polls won't transform themselves into action, either on their own or through Napolitano, but instead only through people organizing, bucking the ridiculous red-state/blue-state political straightjacket and pushing from below.<br>
<b>Jeff Bale, </b>Phoenix<p>

