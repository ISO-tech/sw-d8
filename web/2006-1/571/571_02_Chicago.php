<font face="Times New Roman, Times, serif" size="5"><b>Chicago rejects cheap fuel</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 13, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">VENEZUELAN PRESIDENT Hugo Ch&aacute;vez's offer of discounted oil and fuel to help poor Americans is a brilliant way to show up both the Bush administration's hatred of radicalism in Latin America and its contempt for the have-nots in this country.<p>
Citgo Petroleum, which is owned by the Venezuelan government, made an arrangement with nonprofit organizations in New York City and Boston to deliver millions of gallons of heating oil at roughly half the market cost. The company estimates that tens of thousands of low-income and elderly residents will benefit.<p>
But the Democrats' one-party state in Chicago is having none of it. In October, Citgo Petroleum, which is owned by the Venezuelan state, met with city officials to offer cheap diesel fuel to the Chicago Transit Authority (CTA)--on the condition that the CTA pass the savings on to poor residents through free or discounted fares. According to CTA spokesperson Ibis Antongiorgi, however, the transit system has "no intent or plan to accept the offier."<p>
Instead, the CTA plans to deal with a looming budget shortfall by <i>raising </i>bus and subway fares. Riders will face a 25-cent-a-ride increase, and those paying cash won't be able to get discounted route-to-route transfers.<p>
"This is going to hurt the poor and the minority people, like me," Humboldt Park resident Dorothy Chew, told the <i>New Standard </i>Web site. Chew, who relies on the CTA to get to work and to school, said she'll be hit by both the fare increase and the elimination of discounted transfers--since she rarely to invest in a fare card.<p>

