<font face="Times New Roman, Times, serif" size="5"><b>UVM wants to lay off faculty</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nancy Welch, delegate, UVM United Academics-AFT/AAUP</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 13, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">BURLINGTON, Vt.--Even as full-time faculty at the University of Vermont (UVM) were voting to ratify a new three-year contract, pink slips were appearing in the mailboxes of six long-time lecturers.<p>
Five senior lecturers and one lecturer in the university's education department received the layoff notices just before the December holiday break. The layoffs are scheduled to take effect in May. In an emergency meeting, union delegates learned that as many as 21 lecturers in the department--more than half the teaching faculty--may receive termination notices in the coming months.<p>
The layoffs appear to be a test of the protections won for lecturers since UVM's full-time faculty unionized in 2001, affiliating with the American Federation of Teachers (AFT) and the Association of American University Professors (AAUP). <p>
The six lecturers, all of them women between the ages of 50 and 60, have earned top performance ratings in their eight to 12 years of employment. They had been scheduled to teach a combined 56 courses next year for more than 800 undergraduates. <p>
Delegates at the emergency meeting resolved to oppose the layoffs and, importantly, discussed the need to unite faculty in publicly defending these jobs. "We work for the rights of everybody," Peter-Jack Tkatch, a theater professor, told <i>Socialist Worker.</i> "That's what it means to be in a union: All for one and one for all."<p>
Under the new contract, faculty can expect average yearly raises of 5.42 percent. That's slightly higher than the annual increases realized under the last contract and significantly higher than the average wage increase of 3.7 percent negotiated by other faculty unions in the past year. <p>
It keeps UVM faculty on a slow track to closing the 15 percent wage gap between its salaries and the average for state university faculty nationwide. But the raises are accompanied by a cut in health care benefits, including new co-pays of $100 to $250 for outpatient surgeries and hospital stays, and new language that gives UVM the right to put proposed cuts to retiree benefits before the state Labor Relations Board.<p>
Although unions nationwide have taken hits in health care and retiree benefits, UVM faculty had good reason to hope for better. The university, far from suffering fiscal crisis, has funded the six-figure salaries of 16 new vice presidents in recent years as well as given the green light to half a billion dollars in new construction.<p>
A straw poll of delegates taken earlier in the fall showed that a sizable majority opposed exchanging health care benefits for higher salaries. Despite this resolve, the union's public campaign was muted by members' uncertainty about voicing their major contract aims publicly and collectively. <p>
No broad-based committee was formed to plan actions in support of the negotiating team, and the work of publicizing contract issues to members and the community fell to just two or three people. Even so, the handful of events that did take place showed strong potential, including a November march by 200 students, faculty and staff into the administration building where the board of trustees was in session.<p>
The trustees, who reportedly complained to university president Daniel Mark Fogel that they were growing weary of the escalating protests at trustee meetings, and the salary increases in the new contract are evidence that a better contract is won when a negotiating team is backed by a mobilized membership. <p>
The cuts in health care and the pink slips handed to education lecturers underscore the need to keep fighting--to defend the new contract and the union's hard-won protections for lecturers.<p>

