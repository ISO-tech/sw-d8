<font face="Times New Roman, Times, serif" size="5"><b>They call this "progress" in Iraq</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 13, 2006 | Page 1</font><p>

<font face="Times New Roman, Times, serif" size="3">FOLLOWING A meeting with his national defense team and top military leaders, George W. Bush told reporters last week that "2005 was a year of progress toward meeting our goal of victory." But even as he spoke, one of the bloodiest periods in Iraq in months was unfolding.<p>
A series of bombings and guerrilla attacks left as many as 200 Iraqis dead over the course of two days last week.<p>
In Muqdadiya, northeast of Baghdad, a bombing during a funeral killed more than 50 on January 4. The following day, an attack outside a Shiite shrine in Karbala left more than more than 60 dead, while in Ramadi, a suicide bombing near a military base killed more than 50--many of them police recruits--even as Marines with bomb-sniffing dogs were screening the jobseekers. <p>
For the U.S. military, too, last week included one of the deadliest four-day stretches since the 2003 invasion--with at least 16 U.S. GIs and Marines killed in roadside bombings and fighting west of Baghdad.<p>
Until last week, the Bush administration had proudly pointed to relative calm during the December 15 elections in Iraq as proof that the occupation was on the right path. "[T]hose who want to stop the progress of freedom are becoming more and more marginalized inside of Iraq," Bush claimed.<p>
In reality, the elections only heightened ethnic and religious divisions as the U.S. pitted Shiite Muslims--60 percent of the population--against the country's Sunni Muslim minority. <p>
A recent anonymous comment--from "an American currently working in Baghdad for a news organization"--sent to Iraq expert Juan Cole's Web site summed up the dire situation. "The current security situation here has gotten much worse since the elections," the comment read. "[W]e were told that there's a high probability of all-out civil war...Instead of the scores of Iraqis dying each day, as they do now, thousands a day could perish. <p>
Most Sunnis have given up hope of getting adequate representation in the new Iraqi government, and radical elements in the Shiite parties want to exact revenge on the Sunni for supporting Saddam over the years. Shiite death squads roam the city at night (in police and army uniform no less), dragging all the male members of a Sunni family out into the street and executing them in front of their womenfolk. Sunni insurgents (not in uniform) do the same to Shiite families in areas claimed as theirs."<p>
This kind of chaos is the inevitable result of the Bush administration's "divide-and-conquer" strategy.<p>
According to Cole, while Shiite death squads appear to be operating in the police forces in some areas of the country, in others, the U.S. appears to be cutting deals to allow Sunni guerrillas to run police units. <p>
"Iraqi guerrillas were especially upset about the bombing of potential police recruits in Ramadi, since some of the men belonged to the Sunni Arab guerrilla movement," reported Cole. "The guerrillas had given them permission to enlist under a secret agreement they had reached with the Americans via the mediation of tribal chieftains, stipulating that the guerrillas would dominate the security services, the police and army in the Sunni Arab provinces, as an element in an overall settlement. The guerrillas would be able to place their men in the security services of Anbar, Salahuddin and Ninevah provinces. <p>
Meanwhile, the U.S. continues to pursue its policy of "Iraqification"--relying increasingly on new Iraqi police and military recruits to carry out the dirty work.<p>
The Bush administration may have counted it a minor victory, for example, that the death toll of U.S. forces in Iraq in 2005 (846) didn't exceed 2004 (848). But that doesn't take into account the death toll suffered by Iraqi police forces. According to the Iraqi Interior Ministry, at least 1,225 policemen and 475 soldiers were killed last year alone--a number that even the government admits is likely undercounted.<p>
In other words, Iraq isn't becoming more "stable." The violence is getting worse--and the U.S. is helping it along. <p>

