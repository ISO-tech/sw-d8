<font face="Times New Roman, Times, serif" size="4">New groups aim to unite workers across the industry</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Taking on the airlines</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 13, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">THE AIRLINE industry's onslaught against workers shows no signs of letting up. Airline executives are using bankruptcy courts and the threat of bankruptcy to force billions of dollars worth of wage and benefit concessions from unions.<p>
Striking mechanics at Northwest Airlines (NWA)--the first group of workers to decide that they couldn't stomach even more concessions--are now into their fourth month of picketing, but other airline unions and even other unions at NWA have yet to provide them with any substantial material solidarity.<p>
To help build support for the NWA strike and to address the burning need for a coordinated labor response to the industry onslaught, two new efforts have recently been launched.<p>
In November, 27 labor activists formed Airline Workers United (AWU) to bring together workers from different airlines, such as NWA, United and American, and different workgroups, including mechanics, flight attendants and ground workers.<p>
The same month, the Aircraft Mechanics Fraternal Association (AMFA) launched the Campaign to Save Airline Jobs and Safety with rallies of several hundreds in Minneapolis and Detroit. The campaign aims to put pressure on the bankruptcy court, NWA's executives and Michigan Gov. John Engler, who's a member of NWA's board of directors.<p>
Here, JENNIFER BIDDLE, a laid-off aircraft mechanic who worked at United and Alaska Airlines, talks with <i>Socialist Worker </i>about what AWU hopes to accomplish.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>WHAT IS AWU's vision about how to fight back against the assault on airline unions?</b> <p>

AWU'S VISION is very simple: it's all about solidarity. In concrete terms, that means everything people organize around on the job has to work towards building solidarity. <p>
By choice and by chance, workers in the airlines are split into an infinite number of unions, even within one skill group. Through organizing together across work groups, AWU hopes to actively show what solidarity is really about.<p>
NWA mechanics showed us the ultimate act of solidarity and sacrifice when they rejected the airline's most recent offer. The offer was the first they had had a chance to vote on since the strike began last August. <p>
As pitiful as it was, mechanics could have taken the severance pay, unemployment benefits and recall rights. After all, the other union leaders had been promoting a "me, me, me" philosophy with their own members, and the mainstream press had already written the strike's epitaph. <p>
But the mechanics opted to keep the fight going instead. This leaves open the possibility that others could join their fight. <p>

<b>THE AWU is organized around the idea of bringing together people from different unions in the industry, including rivals such as the IAM and AMFA. Why is this important?</b> <p>

THE ONLY reason bad blood exists between these unions is because the old, established union leadership--in particular the machinists--look at their members as numbers, as dues money, as objects to be controlled, and not individuals with rights and ideas of their own. Jurisdiction is the end all and be all for them. Unions founded on the principles of democracy, like AMFA, are a threat to the idea that union members should bow down to their leaders.<p>
One of the first things I learned as a mechanic in aviation school, and the basis for jet engine propulsion is Newton's Third Law of Motion--for every action there is an equal and opposite reaction. AWU believes the only way to counter the undermining of solidarity--like the active scabbing of work by the machinists at NWA--is to build solidarity. <p>
I'm very proud of the fact that several of the founding members of AWU are machinists at NWA who are on sympathy strike with the mechanics. Ultimately, it is up to us to reach out to everyone who still believes in and is willing to act on the idea of solidarity.<p>

