<font face="Times New Roman, Times, serif" size="5"><b>Shot by police in New Orleans</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 13, 2006 | Page 2</font><p>
<font face="Times New Roman, Times, serif" size="3"><b>BARRY SCOTT reports from New Orleans on the police shooting of an African American man.</b><p>

RACISM IN New Orleans was on the display over the holidays when police killed an African American man in a hail of bullets. <p>
On the day after Christmas, three officers fired nine shots at 38-year-old Anthony Hayes after he refused orders to put down a small pocketknife. The shooting occurred after Hayes allegedly punched or slapped an employee of a Walgreen's store. <p>
Videos showed Hayes shows Mr. Hayes, armed with a small pocketknife with a 3-inch blade, surrounded by at least 12 officers. More than 30 police vehicles appeared on the scene. After a failed attempt to subdue him with pepper spray, Hayes was forced backward another block by the police surrounding him. There, he was shot and killed. <p>
Police spokesman Sgt. David Adams confirmed that three officers fired nine shots at Hayes. "You have a subject who's lunging at them with a knife...swinging wildly at them," Adams said. "They had no other choice but to resort to lethal force." Superintendent Warren Riley said in a statement, "We're not proud of the incident, but we're certainly happy that we're not burying a police officer this week."<p>
Monique Champagne, one of three people who filmed the incident, said, "There were so many cops there, I thought surely this guy just shot a cop." "At first, it was real quiet and slow," Champagne said. "Then faster and faster, more cops showed up. Then one gun went off, and then a whole bunch went off."<p>
She said she was too far away to see the shooting, but the idea that Hayes attacked the officers wasn't consistent with the behavior she saw earlier. "I think it was an injustice," she said. "That guy shouldn't have died."<p>
At the initial encounter two blocks away, when multiple officers first leveled their guns at Hayes, a Burger King employee recognized the man. <p>
Michelle Dawson said Hayes always came in for coffee and a biscuit. Sitting by himself next to the window for hours at a time, Dawson said he "talked to himself and dragged on." She said she had "never seen that man bother anybody." Asked if she saw the knife in his hand as police moved in, Dawson said, "I didn't see any knife, this man had his hands up." <p>
Members of Hayes' family say he was schizophrenic, and that there were enough police present to subdue him without the use of deadly force.<p>
Trey Brokaw, who was in a nearby bar, said he saw the victim with a knife in his hand shortly before the shooting, but that he "didn't see anyone near him. It didn't seem to me like anyone was going to get hurt." <p>
Details are unclear as to who called the police. Some reports say a local businessman; another, an off-duty St. Bernard Parish sheriff's deputy who knew about the Walgreen's incident and flagged a police car. <p>
But the video of Hayes' shooting has put the spotlight back on New Orleans Police Department after multiple allegations of looting and worse during the Hurricane Katrina disaster, and the videotaped beating of a retired schoolteacher. <p>

