<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">January 13, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Pratt Institute</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Cindy Klumb, steward, OPEIU Local 153</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--After several months of unsuccessful contract negotiations, a tentative date of January 12 has been set for a strike authorization vote by members of Office and Professional Employees International Union (OPEIU) Local 153 at Pratt Institute in Brooklyn.<p>
OPEIU Local 153 represents the clerical, secretarial and technical academic support staff at Pratt, and their contract expired June 30, 2005.<p>
An earlier vote scheduled in late December was postponed at the request of the administration, which wanted another chance to meet. Negotiations lasted into the evening, but no agreement could be reached on the major economic issues. <p>
The administration is offering raises lower than those received during the worst years--even though they have more than $10 million dollars in surplus income over expenditures last year. <p>
The union is asking for a one-time wage adjustment of 15 percent over two years to make up for some of the economic ground lost in the last decade, which amounts to $337,000 or about .1 percent of Pratt's total annual operating budget. The two-year additional wage increase for 72 employees is comparable to what President Tom Schutte receives in annual compensation. <p>
Last fall, the union organized several actions, including an informational picket on Parents Day and National Alumni Day. We have received verbal support from the department chairs, deans, the Academic Senate and the faculty union. <p>
At the "State of the Institute" address in December, members posed difficult questions to President Schutte. <p>
In the 1990s, Pratt was on the verge of bankruptcy, and Local 153 gave up dental benefits, froze minimum salaries from 1992 to 2000 and accepted minimal pay raises over three contracts that have not kept up with inflation to help Pratt make ends meet. Today, academic support staff is paid about 20 to 28 percent below the market rate depending on the position. <p>
Even though many members can't afford to strike, they also can't afford not to. Our members can't afford New York City rents with one of their biweekly checks, and most must seek additional employment to supplement Pratt's inadequate salaries. <p>
The recent transit strike has shown that withholding labor is still the most effective weapon workers have against the assault on labor by employers.<p>

