<font face="Times New Roman, Times, serif" size="4">Bush plans to get around new Senate law</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The not-really torture ban</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 13, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">ON DECEMBER 30, George W. Bush signed a new law that bans the use of torture by the U.S. spy agencies and the military--sort of.<p>
The bill, sponsored by Republican Sens. John McCain (R-Ariz.), John Warner Jr. (R-Va.) and Lindsey Graham (R-S.C.) passed the Senate in a resounding 90-9 vote in October. But when Bush signed the measure a few weeks ago, he also issued a "signing statement"--an official document defining the president's interpretation of a new law.<p>
"The executive branch shall construe [the law] in a manner consistent with the constitutional authority of the President...as Commander in Chief," Bush wrote.<p>
In other words, Bush--and Bush alone--will decide whether torture is necessary.<p>
"Of course, the president has the obligation to follow this law, [but] he also has the obligation to defend and protect the country as the commander in chief, and he will have to square those two responsibilities in each case," a senior administration official, speaking anonymously, told the <i>Boston Globe.</i> "We are not expecting that those two responsibilities will come into conflict, but it's possible that they will."<p>
This "what Bush says, goes" scenario is sounding more and more familiar. Since September 11, the administration has overridden domestic and international laws in detaining prisoners captured in the war in Afghanistan and claimed the authority to hold any U.S. citizen considered an "enemy combatant" without charges or access to an attorney.<p>
Last month, it was revealed that the administration believes Bush has the authority to authorize the National Security Administration to wiretap U.S. citizens without a warrant.<p>
When Attorney General nominee Alberto Gonzales testified at his confirmation hearings a year ago, he said he believed anti-torture laws didn't apply to interrogators at overseas prisons--because the Constitution doesn't apply abroad. McCain responded with an amendment to a Defense Department bill banning cruel, inhumane and degrading treatment of detainees in U.S. custody, wherever they are being held.<p>
"I do not believe that any political figure in the country has the ability to set aside any...law of armed conflict that we have adopted or treaties that we have ratified," said anti-torture bill cosponsor Graham. "If we go down that road, it will cause great problems for our troops in future conflicts because [nothing] is to prevent other nations' leaders from doing the same."<p>
Meanwhile, recent reports have revealed the horrific treatment of prisoners on hunger strike at Guant&aacute;namo Bay. According to lawyers, they have been tied down and force-fed through tubes forced down their nasal passages into their stomachs.<p>
And just weeks ago, Bush signed into law a bill removing detainees' right to file habeas corpus petitions in the U.S. federal courts. On January 6, the White House requested that the Supreme Court make this measure retroactive--nullifying about 220 cases in which prisoners contested detentions and the legality of their pending trials.<p>
This is the real face of American "democracy." It applies only when it's convenient for the Bush administration.<p>

