<p><a name="Bush"></a><font face="Times New Roman, Times, serif" size="4"><b>Less popular than Bush</b></font><p>

<font face="Times New Roman, Times, serif" size="3"><i>SOCIALIST WORKER </i>often points to President Bush's approval ratings when diagnosing the political pulse of the country. Yet despite Americans' continued rejection of Bush on every position--from the war to Social Security to tax cuts, etc.--he has continued to govern with unbridled arrogance. <p>
Most recently, he has loudly defended the practice of spying on U.S. civilians (including antiwar activists) and quietly reneged on the promised aid to the hurricane-ravaged Gulf Coast, with the package shrinking from $200 billion to $60 billion.<p>
In the face of this, the Democrats over the past four years have nary lifted a finger. At best, they have presented inaction--at worst, they have provided margins of victory for Bush-supported legislation. They have rejected the antiwar movement. They have backtracked on abortion. They have voted for tax cuts and the USA PATRIOT Act. And on and on. <p>
<i>SW </i>has often exposed the political bankruptcy of the Democrats' strategy and explained their true role as the Other Ruling Class Party. But I think equally important to understanding the current political climate is to look at the ramifications of the Democrats' strategy.<p>
The result has been this: people in the U.S. have not just rejected the Bush agenda, they have also rejected the Democrats' inaction. <p>
While Bush has fallen in popularity, he was able to beat John Kerry in the 2004 election. And even today, despite his popularity flagging even further since his reelection, Bush enjoys a greater popularity than the Democratic Party. Congress as a whole has an approval rating of between 25 to 30 percent--10 points below Bush--according to recent Harris Interactive polls. But within that, the Democrats are even lower, with a rating of just 25 percent!<p>
Rather than gain ground as Bush has fallen, the Democrats have dropped just as far. In October 2001, Democrats in Congress boasted an approval rating of 68 percent. Today, Democrats have a lower rating than they did in 1994, just after the so-called "Republican Revolution" swept a whole host of Democrats out of Congress.<p>
To me, the numbers help explain how Bush can govern so confidently despite his low popularity. Even though Americans are rejecting his policies and the war, he is benefiting from the fact that the "opposition party" is unwilling and unable to do what it would take to win support.<p>
For progressives, the implications couldn't be clearer. In Bush's biggest moments of vulnerability, the Democrats have failed to present any semblance of opposition. They represent a political dead end. <p>
The American people are rejecting the Democrats. The biggest mistake we could make is to revive them through hopeless efforts to pull the party to the left. Now is the time to rebuild an independent left in this country. The opening for an alternative is greater than it's been in a long, long time.<br>
<b>Petrino DiLeo, </b>New York City<p>

