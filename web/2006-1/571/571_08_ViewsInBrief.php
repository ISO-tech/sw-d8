<font face="Times New Roman, Times, serif" size="4"><b>A sick health care system</b></font><p>

<font face="Times New Roman, Times, serif" size="3">MY FATHER recently checked into the emergency room of Columbia Presbyterian (run by Columbia and Cornell Universities) because his hip replacement broke, causing him extreme pain whenever he tried to walk, get up or sit down. <p>
Despite this, he was denied admittance to the hospital. The reason? It was "not urgent" that he receive medical attention. Apparently, being unable to walk is no longer considered an emergency situation in the emergency room.<p>
To make matters worse, the next day the same hospital that gave him the boot tried to charge him a $300 fee up front just to talk to one of its doctors, since the hospital doesn't take his insurance. This is ridiculous, since my father's insurance is provided by his employer--the government of New York state.<p>
So, even if you have health insurance, you can still get turned away in an emergency room. This situation is inevitable as long as control of the health care system is in the hands of profit-driven corporations, insurance companies and their armies of cost-cutting bureaucrats--and not in the hands of workers, nurses, doctors and patients.<br>
<b>Pham Binh,</b> New York City<p>

