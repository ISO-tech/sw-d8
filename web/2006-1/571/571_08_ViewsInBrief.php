<p><a name="NSA"></a><font face="Times New Roman, Times, serif" size="4"><b>Their war on our rights</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THE NSA has listened in on American phone calls without warrants. The Pentagon has spied on antiwar and religious groups. Local police departments have started (or expanded) intelligence units. And, thanks to the USA PATRIOT Act, the FBI can still look at your library and medical records without your consent. <p>
How does spying on innocent Americans stop foreign terrorists?<br>
<b>Chuck Mann, </b>Greensboro, N.C.<p>

