<p><a name="NY"></a><font face="Times New Roman, Times, serif" size="4"><b>Solidarity in New York</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THANKS FOR the terrific coverage of the recent New York City transit strike. <p>
A Los Angeleno in New York for the holidays during the strike, I was inspired by the overwhelming support for the strikers by ordinary New Yorkers. Everything I had read in the national media was so lopsided. Upon arriving in the city I was pleasantly stunned to hear taxi drivers, shopkeepers, waiters and students so livid toward Mayor Michael Bloomberg, Gov. George Pataki and their ilk. <p>
My question is this: Why not keep the trains running for free during this kind of struggle?<br>
<b>Rob Zenchelsky</b>, Los Angeles<p>

