<font face="Times New Roman, Times, serif" size="5"><b>War crimes and hunting misdemeanors</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 24, 2006 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">MASTERMIND A war justified with lies that causes the deaths of at least 100,000 Iraqis, and the corporate media shut up and repeat the Pentagon line. Injure a millionaire Republican with a shotgun blast to the face while hunting with the rich and powerful, and the media are tireless and combative in pursuit of the truth.<p>
Dick Cheney had the toughest week of his five years as puppet master of the Bush White House after the news finally emerged that he nearly killed a 78-year-old Republican power-broker while "hunting"--or, to be more precise, blasting away at coveys of quail flushed by servants on horseback--at the Texas mega-ranch of a well-connected lobbyist.<p>
The media devoted endless pages and hours of airtime to documenting the timeline of events at the Armstrong ranch in south Texas, near the border with Mexico. Their uproar at the delay in getting the details of what happened--and then at the contradictions in the official story--was so great that Cheney was forced out of his bunker to answer questions in a TV interview, albeit with Brit Hume of Fox News.<p>
This from the same media establishment that wholeheartedly fronted for the administration's claims about weapons of mass destruction in Saddam Hussein's Iraq; that, at the request of the White House, held up publication of revelations about government spying on U.S. citizens; that regularly repeats the Republican p.r. machine claim that Bush and Cheney are salt-of-the-earth regular guys.<p>
Not that the shooting and its aftermath didn't highlight all the qualities we've come to expect from Cheney and the Bush administration--arrogance, obsessive secrecy and sleazy attempts to avoid blame for anything that goes wrong.<p>
Cheney's near-whacking of lawyer Harry Whittington was kept hushed up for almost a full day before details were released--and then, only to a small South Texas newspaper. Cheney himself stayed silent while ranch owner Katharine Armstrong lied about no one drinking during the hunt and insinuated that Whittington was to blame for getting shot. Meanwhile, a Christian fundamentalist doctor downplayed the seriousness of Whittington's injuries, which landed him in intensive care twice.<p>
Still, all this pales in comparison to the much more serious crimes that Dick Cheney has committed--continually, as a matter of course--during his political career.<p>
Though you'd never know it from the deferential way he's treated today, Cheney has a long record as a right-wing kook--going back to his time as a staffer in the final days of the Nixon White House, and then as a congressman from Wyoming.<p>
To take but one example, in 1985, Cheney voted against a House resolution calling on the racist apartheid regime in South Africa to release Nelson Mandela after 23 years of imprisonment. Over the next few years, Cheney voted against various forms of sanctions against apartheid at least 10 times.<p>
Drafted into George Bush Sr.'s administration as defense secretary in 1989, Cheney helped to run the first Gulf War slaughter against Iraq, pushing for an air war and then a ground offensive against all objections. More than 1 million Iraqis paid with their lives during the war and the decade of UN sanctions that followed.<p>
As vice president under Bush Jr., Cheney is generally credited with pulling the strings on any issue that he wants to. He was the most insistent administration voice claiming that Iraq possessed weapons of mass destruction--and that Iraqis would welcome U.S. soldiers as liberators.<p>
He should therefore bear some significant part of the blame for the deaths of more than 100,000 Iraqis and over 2,000 U.S. soldiers. But you won't hear the mainstream media go after Cheney for his war crimes, only his hunting misdemeanors.<p>
Consider some of the revelations that passed almost unnoticed last week during the uproar over Cheney's hunting accident.<p>
Rumors of death squads operating within the Iraqi police forces and targeting minority Sunni Muslims were confirmed when the Interior Ministry promised to open an investigation after U.S. troops stopped a 22-member death squad on its way to execute a prisoner.<p>
Meanwhile, the CIA's national intelligence officer for the Middle East for the five years confirmed that intelligence on Iraq was "misused publicly to justify decisions already made" during the run-up to the invasion.<p>
And Dick Cheney's former aide, Lewis "Scooter" Libby, who resigned in disgrace after being indicted in the CIA leak scandal, reportedly told a federal grand jury that he was authorized to leak sensitive intelligence to reporters by...none other than Cheney himself.<p>
Cheney and the Bush administration have committed an endless string of real crimes--and they should be held accountable for them.<p>

