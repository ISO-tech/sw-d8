<font face="Times New Roman, Times, serif" size="4"><b>When cops sue cops</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THE PROTESTERS were threatened with arrest for standing on the sidewalk and herded into pens. The police harassed them for handing out fliers, and a special unit videotaped their faces, stoking their anger even higher, as they dogged the mayor at his appearances across the city. "That's Big Brother watching you," Walter Liddy, one of the demonstrators, said in a deposition.<p>
Liddy's grievances about strong-arm police tactics echo the common experiences of activists all across the U.S. What's unusual about Liddy is that he doesn't fit the typical profile of a protester. In fact, he's a New York City cop.<p>
In the summer of 2004, Liddy and fellow cops organized rallies to protest stonewalling by the city in the negotiations for a new contract. Now the officers--with the help of the Patrolmen's Benevolent Association--are suing the city. Their claim is that the police tactics used against them--identical to those used against protests for progressive causes--violated their First Amendment right to freedom of speech and assembly.<p>
Liddy's charge that he and other off-duty cops faced the "constant threat of arrest" duplicate the complaints of social-justice activists, who say the city has essentially "criminalized dissent," especially since September 11.<p>
Two other cases involving similar allegations are pending, but the police case is striking because the people who impose restrictions on other protests are saying that the city's new "antiterrorism tools" have been illegally used to curtail <i>their </i>civil rights.<br>
-- <b><i>New York Times</b>, </i>February 3, 2006<p>

<font face="Times New Roman, Times, serif" size="4"><b>Not a rocket scientist</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NASA PUBLIC affairs officer George Deutsch resigned in disgrace earlier this month, after being exposed for trying to censor some of the country's top scientists.<p>
Deutsch was given a job at NASA after working on George Bush's 2004 re-election campaign, and apparently decided to impose the administration's views about global warming and evolution. He removed information from NASA's Web site showing that 2005 was the warmest year on record, and he forced a Web designer to insert the word "theory" after every mention of the phrase "big bang." <p>
According to Deutsch, the "theory that the universe was created by a 'big bang' is just that--a theory. It is not proven fact; it is opinion. Yes, the scientific community by and large may share this opinion, but that doesn't make it correct...It is not NASA's place, nor should it be, to make a declaration such as this about the existence of the universe that discounts intelligent design by a creator--the other half of the argument."<p>
In January, James Hansen, NASA's top climate scientist said that the Bush administration--and Deutsch in particular--had tried to stop him from speaking out about global warming. According to Hansen, Deutsch rejected a request from a producer at National Public Radio to interview Hansen, called NPR "the most liberal" media outlet in the country, and reportedly said that his job was "to make the president look good."<p>
As it turns out, Deutsch wasn't let go because of his backward pseudo-scientific views or trying to muzzle Hansen. He was forced out after he was caught padding his resum&eacute;--saying that he had a journalism degree from Texas A&amp;M university, when he in fact never graduated.<br>
-- <b>DemocraticUnderground.com</b>, February 13, 2006<p>

