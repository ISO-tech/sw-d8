<font face="Times New Roman, Times, serif" size="5"><b>Muslims under attack in Michigan</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 24, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE CONSEQUENCES of the racist "war on terror" have become clear in Michigan, with a series of attacks on Muslims in the Detroit area.<p>
Imam Muhammad Uddin, the clergyman for the Al-Islah Islamic Center in Hamtramck, Mich., was beaten recently by three people wielding a snow shovel. Three days later, a group of young people reportedly entered the Al-Islah center, took shoes that congregants had left by the door, and began throwing them at worshippers.<p>
Teenage boys, sometimes in groups of five or six, have reportedly been harassing members of the large mosque daily in recent weeks, and windows at the same mosque have been broken by rocks and snowballs twice already this year. "We are in a very bad situation," Abdul Motlib, the mosque's president, told the <i>Detroit Free Press</i>.<p>
According to the Council on American-Islamic Relations (CAIR), 15 Muslim merchants in the area also reported being mugged in a two-week span of time in early February. Dawud Walid, CAIR's executive director, wondered if these attacks are a backlash against TV news depictions of Muslims protesting racist cartoons published in Denmark. "It would be just conjecture," said Walid, "[but] we do know there is constant religious and racial tension in the area."<p>
In 2004, the Al-Islah mosque was thrust into the news when Hamtramck tried to pass an ordinance to regulate the amplified call to prayer from mosques. The issue became a referendum on free speech and freedom of religion--and voters upheld an amendment to the ordinance that specifically allowed mosques to issue the call.<p>
Today, however, city officials are downplaying the recent attacks. Hamtramck City Manager Donald Crawford told the Associated Press last week that he was unaware of any assault on the imam--and that, since the city has many Arab-American businessmen, it wasn't unusual for them to be crime victims.<p>
And according to Hamtramck Police Chief James Doyle, there's nothing out of the ordinary with vandalism against the mosque. "There is no indication that it was racial or ethnic," Doyle said. "It's a case of somebody finding a broken window."<p>

