<font face="Times New Roman, Times, serif" size="5"><b>U.S. military crimes in Japan</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 24, 2006 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">A U.S. sailor was arrested recently for the murder of Yoshie Sato in Yokosuka, Japan. The 56-year-old woman was beaten to death and robbed of about $129 on her way to work.<p>
The sailor, William Oliver Reese, 21, was based on the aircraft carrier USS Kitty Hawk and has admitted to the crime. Witnesses at his confession said that Reese had asked for money for a taxi and became enraged when Sato pushed him away.<p>
Prime Minister Junichiro Koizumi urged the U.S. military to work harder to prevent such crimes. Fat chance. But the U.S. Navy does have a plan. Although this murder was committed in the morning, a three-day curfew was imposed requiring Navy personnel to be back on base by midnight.<p>
A Navy press release also announced that the "deeply saddened" Navy community would implement a "period of reflection to collectively demonstrate sympathy for the tragic loss of life."<p>
This case is considered extremely sensitive because the U.S. fears that such incidents will add to the already intense opposition to plans to build an American military airstrip in a highly populated area of Okinawa and to base a U.S. nuclear-powered warship at Yokosuka.<p>
U. S. Ambassador Thomas Schieffer's statement that "the American people are deeply shocked and saddened by this event" rings about as hollow as the U.S. military's statement of regret about the recent murder of a family of 12 in Iraq in its search for insurgents.<p>
Meanwhile, a Japanese cab driver responding to a call recently was robbed by a knife-wielding man who came out of a Marine barracks in Okinawa. A Marine Corps spokesman said the usual, "The Marine Corps takes this incident very seriously and is working in close cooperation with the local authorities."<p>
On Christmas morning a 72-year-old Okinawan woman found a drunk Marine asleep in her son's bed. Another Marine, in an alcohol-related incident, damaged a $100 taxi sign when the driver told him he was not authorized to take him to his base. Two other inebriated Marine lance corporals were arrested in incidents involving vandalism to a taxi and obstructing justice.<p>
"The Marines on Okinawa take the use of alcohol very seriously," said a Marine 2nd lieutenant. "We have several programs that we utilize to help express the importance of using alcohol responsibly."<p>
It's hard not to laugh at the hypocritical statements of regret by the U.S. empire, but the toll the increased noise and crime occupying forces take on local populations is immense.<p>
Intense grassroots efforts in Japan to stop the assault by U.S. forces against their culture and way of life are the only reason these insincere statements of regret are made and the only reason news of these incidents might make a tiny paragraph on the inner pages of an American newspaper.<p>
While the U.S. does not officially occupy Japan today, the Japanese people are forced to accept the presence of U.S. troops they want out while their leaders make cozy deals with Washington. Just as in Iraq, it's way past time for the U.S. troops to go!<br>
<b>Cindy Beringer, </b>Austin, Texas<p>

