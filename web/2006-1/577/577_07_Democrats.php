<font face="Times New Roman, Times, serif" size="4">Effort to stop Sheehan's primary challenge shows:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Real Democrats" are pro-war</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 24, 2006 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">RECENT OPINION polls show that nearly two-thirds of the U.S. public doesn't support George Bush. More than 60 percent of the public wants all or some U.S. troops withdrawn from Iraq, and 55 percent consider the war a "mistake."<p>
Yet the Democrats seem to be doing their best to avoid any hint that they might want to ally themselves with those popular majorities.<p>
Take the news that after briefly considering running in the Democratic primaries against pro-war Sen. Dianne Feinstein, antiwar activist Cindy Sheehan decided not to run. While attending the World Social Forum in Venezuela, Sheehan told interviews from the Truthout Web site that she was considering a challenge to Feinstein, who she considered "abysmal" and whose support for the Iraq war Sheehan called "indefensible." Feinstein "is a Republican in Democrat's clothing," Sheehan said. <p>
Considering that the war in Iraq is the single most important reason for the collapse of Bush's popularity since his reelection in 2004, wouldn't the main opposition party want to embrace Sheehan, who has become the most visible symbol of opposition to the war?<p>
Not on your life. Immediately after news spread of Sheehan's intentions, the Democrats--the same party that couldn't coordinate a filibuster among 40 senators against Samuel Alito's Supreme Court nomination--move into action with lightning speed.<p>
Liberal Sen. Barbara Boxer, who voted against authorizing the Iraq war in 2002, was chosen to deliver the coup-de-grace to Sheehan's bid. In a pre-arranged phone conference with key California newspapers, Boxer said: "I don't think having [Sheehan] in the Senate election helps her at all. I think it might have the opposite effect." Sheehan said she wouldn't run shortly thereafter.<p>
But Sheehan's story is only one part of the picture in the run-up to the 2006 congressional elections. The Democrats are determined not to be tagged the "antiwar" or "angry" party, and they have gone to extraordinary lengths to assure that.<p>
In Ohio, Washington insiders forced Paul Hackett--an Iraq war veteran who last August almost won a House seat in a heavily Republican district even after calling Bush a "chickenhawk" and a "son of a bitch"--to drop his primary challenge against former Rep. Sherrod Brown, who is running for the Democratic nomination to run against Sen. Mike Dewine.<p>
Hackett supporter David Mixner told <i>Mother Jones</i> how Washington politicians campaigned against Hackett. "The reasons ranged from he can't win, to he's too controversial, Brown has more money, is more centrist, and more appealing," Mixner said. "It was that inner beltway circle crap. They are people who have no idea what's going on in the country, but believe they know everything."<p>
The pattern is pretty clear. The Democrats aren't interested in carrying an antiwar message, much less an oppositional message, into the fall campaign. Unfortunately, there persists in sections of the antiwar movement the idea that Democrats really do want to end the war--they just can't say it because Republicans will attack them for being unpatriotic.<p>
In fact, Sheehan herself played into this idea when she led a demonstration outside the Chicago offices of Rep. Rahm Emanuel, a leading House Democrat. She told the crowd that "just because someone is a Democrat," they shouldn't expect support from antiwar forces, but that if they want to act like "real Democrats" and end the occupation, then they will have that support.<p>
As the chief recruiter and dispenser of funds to Democratic House of Representatives candidates in the 2006 elections, Emanuel is as key as any Democrat to the party's current approach. He has made it clear that the war in Iraq will not be a key point of difference with the Republicans on which the Democrats will contend in the congressional elections.<p>
When criticizing Bush on Iraq, the Democrats' approved talking points seem to revolve around issues like GOP failure to provide troops with adequate body armor--hardly a clarion call to reverse on the Iraqi disaster.<p>
The plain truth is that when it comes to Iraq, the Democrats are as committed to "victory" as the Republicans. So if the Democrats don't want to mount an antiwar campaign for Congress, the most obvious explanation is that "real Democrats" are not really antiwar.<p>

