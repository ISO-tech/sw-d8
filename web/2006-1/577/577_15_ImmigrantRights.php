<font face="Times New Roman, Times, serif" size="5"><b>NYC immigrants stand up against racist legislation</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lucy Herschel and Aaron Amaral</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 24, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Immigrant Communities in Action, a coalition representing immigrant rights groups from an international array of communities, took advantage of Valentine's Day to organize against racist immigration laws being proposed in Congress. <p>
The group held an Immigrant Letter Writing Fair on February 11. More than 250 immigrants and immigrant rights activists wrote personal letters to Sens. Hillary Clinton and Charles Schumer of New York to demand that Congress pass a just legalization bill for all undocumented immigrants.<p>
In addition, the group is organizing against legislation known as HR 4437 that would make it a felony-level offense to be in the U.S. without status or papers, thus turning 11 million undocumented immigrants into felons overnight. <p>
On Valentine's Day, the groups held a press conference in front of Schumer's office with banners reading "Don't break our hearts, oppose HR 4437 and support real immigrant rights in Congress." Speakers talked about families torn apart for five, 10 or even 15 years--either because they cannot freely travel visit their loved ones in other countries or because parents and other family members have been deported.<p>
"If commodities can travel freely across boarders, then we should be given the same rights," said Carolyn Hermogenes of the Committee Against Anti-Asian Violence. "If we are illegal, then all those who benefit from our labor should be illegal, too."<p>
The group also opposes guest worker programs that have no path to legal status.<p>
While the group had previously met with Schumer and was promised a follow-up meeting, the group was refused access to the office--and was threatened with arrest if they insisted on trying to see him. <p>
With a number of anti-immigrant bills pending in Congress, organizers emphasized that 2006 will be a pivotal year and vowed to use this event as the beginning of an ongoing campaign for equal rights for all immigrants.<p>

