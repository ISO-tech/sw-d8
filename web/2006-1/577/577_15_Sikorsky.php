<font face="Times New Roman, Times, serif" size="5"><b>Strike shuts down Sikorsky</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jay Poppa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 24, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">STRATFORD, Conn.--Workers at Sikorsky Aircraft walked off the job here February 20 in the first strike at the defense contractor since 1963.<p>
The 3,600 workers, members of Teamsters Local 1150, had overwhelmingly voted down the last, best and final contract from Sikorsky, which makes the Black Hawk and other military helicopters. Sikorsky workers are also on the picket line in Palm Beach County, Fla., where the company tests new helicopters for the Army.<p>
 "Corporate greed is the reason for this strike," said Lawrence, a union steward at Sikorsky's Connecticut plant. <p>
In the weeks leading up to the strike, the union put pressure on Sikorsky by setting up a full strike committee of 100 workers. Union members also took part in small workplace disruptions, from blowing air horns in unison to banging on buckets in solidarity, members mobilized to let Sikorsky and their union officials know how they felt about their contract negotiations. <p>
In the final hours of negotiations, Sikorsky withdrew several items from their contract proposals, which were sources of friction between the union and the company. <p>
Management dropped proposals for workers to work a swing shift, which would have eliminated overtime pay, and agreed to contribute more toward workers pension plans. According to the company, its offer included 3.5 percent pay raises in each of the next three years plus a $2,000 bonus.<p>
However, Sikorsky's proposal for workers' health care was not acceptable. It included a doubling of copayments in the first year, plus 15 percent increases over the next two years, eating up the gains in wages.<p>
Workers receive their health insurance coverage from CIGNA health care insurance. According to union sources, United Technologies, which is the parent company of Sikorsky Aircraft, also owns large shares in CIGNA health care and wouldn't accept the union's health insurance proposal, which would use a Teamsters' insurance fund to cover workers. The union proposed insurance would only cost Sikorsky Aircraft an extra $0.31 per worker, per month.<p>
"It's greed," said one worker. "The president of Sikorsky, one person, makes millions of dollars a year before bonuses and can't afford $13,392 a year to give 3,600 people decent health coverage. This is why we need to strike." <p>
Sikorsky reported a net operating profit of $250 million in 2005, a 25 percent increase from the year before. The money comes from the military stepped-up orders for the Black Hawk helicopters and other aircraft used in Iraq and Afghanistan.<p>
With Sikorsky doing so well, their unwillingness to budge on health coverage is just plain corporate greed.<p>

