<font face="Times New Roman, Times, serif" size="5"><b>Death in a "boot camp" in Florida</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 24, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">FOURTEEN-YEAR-old Martin Lee Anderson was so badly beaten that his body was swollen almost beyond recognition as it sat in its coffin.<p>
When Anderson died, he was being held at a Panama City, Fla., boot camp run by the Bay County Sheriff's Office and the state Department of Juvenile Justice--after he was charged with grand theft auto for taking his grandmother's car for a joyride in June.<p>
The cause of his death January 6, according to an autopsy, was internal bleeding from sickle cell disorder. <p>
The day before he died, however, Martin was beaten and restrained by drill sergeants for half an hour--all recorded on videotape. The tape shows nine guards wrestling Anderson to the ground and restraining him, even though the boy appeared to be limp for most of the beating he then received. Martin died the next day in a Pensacola hospital.<p>
"Martin didn't deserve this right here--at all," the boy's mother, Gina Jones, said after viewing the tape. "I couldn't even watch the whole tape." An attorney for the Andersons, Ben Crump, said the guards forced ammonia tablets up the boy's nose in efforts to keep him conscious.<p>
Military-style boot camp programs for juvenile offenders became popular in the 1990s as a part of zero-tolerance policies. The camps tout themselves as places where juvenile offenders can begin "replacing criminal thinking with moral thinking, beliefs and values."<p>
In Florida, they are the brutal leading edge of the criminal justice system--in a state where three teenagers have died in custody in as many years.<p>
Martin's parents are demanding a thorough investigation of his death, and the Florida Southern Christian Leadership Conference is asking for a federal investigation of all deaths in Florida's juvenile system in the last five years. "My baby was murdered," Jones told reporters. "Don't let my baby's death be in vain."<p>

