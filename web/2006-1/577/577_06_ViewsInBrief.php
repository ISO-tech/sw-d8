<p><a name="Why"></a><font face="Times New Roman, Times, serif" size="4"><b>Why criticize progressives?</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AS THE son of a successful business owner, maybe it is impossible for me to truly understand the advocates of a true socialism. However, I consider myself a strong progressive who believes in economic fairness. <p>
I sometimes do not understand your criticism of true progressives like Dennis Kucinich, as well as your disdain for proven liberals like Russ Feingold, Ted Kennedy, and magazines like <i>The Nation</i>. <p>
I only read your paper because my Spanish teacher posts it on his office door, or else I wouldn't even know about it. I really want to understand your perspective. The problem is that I remain unconvinced that regulated capitalism is a problem. I see the perpetual failures of planned economies and see regulated capitalism as the least-evil alternative. <br>
<b>Ryan Tures</b>, from the Internet<p>

