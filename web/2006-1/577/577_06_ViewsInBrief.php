<p><a name="Free"></a><font face="Times New Roman, Times, serif" size="4"><b>Free speech isn't the issue</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IN THE wake of protests in the Islamic world over publishing cartoons of the Prophet Muhammad, many people have posed the question of whether newspapers here should stand up for "freedom of the press" by asserting their right to publish controversial material.<p>
Those who paint the issue in these terms sorely miss the point. Every newspaper makes editorial decisions. In this case, the newspapers in question <i>chose</i> to publish cartoons that cannot be called anything other than racist. The portrayal of a caricature of Muhammad wearing a bomb in his turban makes all Muslims, and by extension all Arabs, out to be violent extremists.<p>
In a climate of racist hysteria against Arabs and Muslims, in which the Bush administration and the FBI rounded up hundreds of peaceful immigrants as a part of the "War on Terror," it takes no courage to publish anti-Muslim cartoons. In many European countries, where far-right xenophobic candidates get a substantial number of votes, and where mainstream candidates regularly capitulate to some of this anti-immigrant sentiment, these cartoons take no more courage than they would here. <p>
To show the absurdity of this outcry over "freedom of the press", imagine if commentators during the McCarthyist witchhunt of the 1950s lamented the inability of cartoonists to publish anti-Communist propaganda. If the press is so free here, why don't they publish cartoons of ordinary Iraqis and Palestinians asserting their right to resist illegal occupation of their lands?<br>
<b>Julien Ball,</b> Chicago<p>
