<font face="Times New Roman, Times, serif" size="4"><b>Dems reject antiwar voices</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IT'S BEEN clear for years now that the Democratic Party is out of touch with the American people's deep anger about the war. But recent decisions show that this is not just about stupidity. If you're really antiwar, the party doesn't really want you.<p>
On February 13, Paul Hackett--an Iraq war veteran who's been a vocal critic of Bush and the Religious Right--angrily ended his campaign bid for the U.S. Senate. In a special election in 2005, Hackett narrowly lost the contest for a seat in the House, getting just under 50 percent of the vote in a Cincinnati district that had never given any Democrat more than 38 percent since 1974. <p>
The writing was on the wall. If an antiwar candidate could have that much impact in conservative, southwestern Ohio, antiwar sentiment must be running deep. The Democrats welcomed Hackett with open arms. But when longtime politician Sherrod Brown entered the race, Hackett said, his friends Sens. Chuck Schumer (D-NY) and Harry Reid (D-Nev.) turned on the heat for Hackett to drop out.<p>
Hackett blasted the party for undermining him, and for telling donors to stop funding his campaign. "For me, this is a second betrayal," Hackett said. "First, my government misused and mismanaged the military in Iraq, and now my own party is afraid to support candidates like me."<p>
You'd better believe it. This is the same party that pressured Cindy Sheehan not to run for Senate in California only a few days before. Antiwar activists should have nothing to do with this sorry excuse for a party.<br>
<b>Pranav Jani</b>, Columbus, Ohio<p>
