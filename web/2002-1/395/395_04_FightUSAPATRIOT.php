<P><font face="Times New Roman, Times, serif" size="5"><b>Their laws would silence us</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 22, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>The Justice Department dusted off the freedom-squelching ideas they've been developing for many years and had them introduced in the USA PATRIOT Act, enacted by Bush on October 26, 2001.</P>
<P>This act is beginning to get some teeth as local governments across the country pass "antiterrorism" bills. Here in Washington, D.C., our Anti-War Network has participated in hearings about the local antiterrorism legislation.</P>
<P>This legislation attempts to classify common crimes as "acts of terrorism"--by judging people's political intentions! According to our local anti-terrorism act, you are committing "terrorism" if you intend to "intimidate or coerce a civilian population" or "influence the policy or conduct of a unit of government by intimidation or coercion."</P>
<P>Penalties associated with "Acts of Terrorism" will be mandatory and, for most, will mean life imprisonment.</P>
<P>It is critical for activists to be aware of when these hearings are being held in our cities. We must be sure to have voices standing against this kind of hypocritical legislation--which upholds peace at the cost of eliminating freedom of dissent.</P>
<P>Find out if local representatives from the National Lawyers Guild and the ACLU will be testifying against this legislation, and rally activist groups from your area to pack these hearings with dissenters.</P>
<P>Our voice against the attacks on our civil liberties here and around the world must be heard!</P>
<B><P>Chris Otten,</B> Washington, D.C.</P>

