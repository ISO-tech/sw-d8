<P><font face="Times New Roman, Times, serif" size="5"><b>The Democrats' warmonger in chief</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 22, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>NEVER ONE to miss out on an opportunity for a little warmongering, Democrat Al Gore jumped on the bash-Iraq bandwagon last week.</P>
<P>Speaking at the Council on Foreign Relations in New York February 12, the 2000 Democratic presidential candidate said that the time had come for a "final reckoning" with Iraq and its leader Saddam Hussein.</P>
<P>Gore applauded Bush for including Iraq in the "axis of evil" portion of his State of the Union address. "As far as I'm concerned, there really is something to be said for occasionally putting diplomacy aside and laying one's cards on the table," he said. "There is value in calling evil by its name."</P>
<P>Threats on Iraq aren't new for Gore, of course. As a senator in 1991, he supported a resolution authorizing Bush's daddy to go to war against Iraq. And of course, he was vice president to Bill Clinton, whose administration backed crippling sanctions on Iraq--and launched regular air campaigns against Iraq.</P>
<P>During the 2000 presidential campaign, he told the American Israel Public Affairs Committee: "We have made it clear that it is our policy to see Saddam Hussein gone. We have maintained sanctions in the face of rising criticism."</P>
<P>Gore is far from alone among Democrats. His running mate in 2000, Sen. Joe Lieberman (D-Conn.), stands out as one of the most fanatical Iraq haters in Congress--Republicans and Democrats alike.</P>
<P>If Gore had any criticism of Republican foreign policy, it was that Saddam Hussein "was allowed to survive his defeat as the result of a calculation we all had reason to deeply regret for the ensuing decade--and still do."</P>
<P>This time, Gore said, "we must absolutely get it right." And the Iraqi people will pay the price.</P>

