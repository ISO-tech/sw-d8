<P><font face="Times New Roman, Times, serif" size="5"><b>The Bush gang has big plans for Utah</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 22, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH was on hand to wave the flag at the opening ceremonies of the Winter Olympics in Utah. But in Washington, his administration was waving around new plans to allow oil drilling in national park land a couple hundred miles away from Salt Lake City.</P>
<P>As part of the Texas oil mob's energy plan, oil companies have been investigating sites for exploration between Arches and Canyonlands National Parks, the beautiful red rock country in southeastern Utah.</P>
<P>The Bureau of Land Management, which supervises federal land, recently told officials in charge of the parks that energy development comes before everything else. "Utah needs to ensure that existing staff understand that when an oil and gas lease parcel or when an application for permission to drill comes in the door, that this work is their No. 1 priority," read a memo from Washington.</P>
<P>The administration policy has given the go-ahead for 50,000-pound trucks to crisscross the delicate landscape in search of oil.</P>
<P>Even oil company officials are a little surprised. "We're all just sort of shaking our head because this area is so controversial," said Beth McBride, president of Legacy Energy, which owns a lease for drilling near Canyonlands. "They shouldn't tell us we can come in and drill if they haven't cleared up all the environmental issues."</P>

