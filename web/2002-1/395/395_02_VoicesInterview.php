<P><font face="Times New Roman, Times, serif" size="4">Crippling consequences of sanctions on Iraq</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"The U.S. is killing people each day"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 22, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>VOICES IN the Wilderness is an international human rights group that has worked to expose the crippling impact of U.S. sanctions on Iraq over the past 10 years. Voices activist RAMZI KYSIA talked to <I>Socialist Worker</I>'s ELIZABETH SCHULTE during his visit to Iraq in January.</P>
<P>COULD YOU describe the impact of U.S. bombing and sanctions?</P>
</B><P>IN BASRA, we stayed in the home of Um Heider, who lost her 6-year-old son in a U.S. missile attack three years ago. Her other son, Mustafa, is maimed from the attack. The family is very poor--no running water, no central heat, electricity half the day, 24 folks from three families in a five-room house. The streets outside are dirt, with sewage trenches running alongside them.</P>
<P>Everyone is very tired of the blockade and the threat of more destruction hanging over them. Most people work three and four jobs to earn as little as $20 or $30 a month.</P>
<P>The hospitals still report massive shortages of critical equipment and medicines. Dr. Raad Towalha at Ibn Sina Hospital in Mosul complained about having new kidney dialysis machines, but no filters for the machines--so he couldn't use them. Some medications will be available one month, but then out the next. Some are never available.</P>
<P>All the hospitals point out repeatedly that in regards to cancer treatment--particularly with childhood cancers, such as leukemia, which have skyrocketed here over the last 11 years--this is not only frustrating, but deadly.</P>
<P>Such cancers have an extremely high cure rate. But if they don't have all of the prescribed drugs, children die unnecessarily.</P>
<B><P>WHO DO Iraqis blame for these conditions?</P>
</B><P>SADDAM HUSSEIN bears total responsibility for the things that he does. Unfortunately for the U.S. propaganda mills, Saddam Hussein isn't blockading Iraq--we are. The Iraqi people must not continue to be punished for the failures of their leader.</P>
<P>The problem in Iraq is two-fold: the inability to fix critical, civilian infrastructures, such as water and sewage treatment, which leads to massive disease outbreaks, and the destruction of the economy, which leads to massive and chronic poverty. Unless both are addressed, mortality rates will not be largely improved in Iraq.</P>
<P>Water and sanitation can't be improved until items that the U.S. claims are "dual-use" [for civilian and military purposes], such as chlorine to disinfect water, are freely allowed into the country. Poverty can't be addressed until sanctions are lifted, because the answer to chronic poverty isn't a better breadline--it's a job.</P>
<P>Iraq is the ultimate in "globalization." It's forced to produce all its income based on a single item that the West desires: oil. That income is totally controlled by the West, and it's used to buy only finished products from companies outside of Iraq, thereby denying Iraq any significant, native industries to employ people.</P>
<P>Everyone here expects the U.S. to attack again at some point. When you ask about a new attack, they say, "What's new about it? The Americans are bombing us and killing people here every day."</P>

