<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 22, 2002 | Page 11</font></p>

<P><font face="Times New Roman, Times, serif" size="4"><b>General Motors</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE LAYOFF ax fell at the biggest of the Big Three U.S. automakers last week when General Motors (GM) announced that it was cutting close to 3,000 jobs at two plants.</P>
<P>Staggered layoffs of some 1,750 workers at GM's Orion Township assembly plant in Michigan will begin in mid-April. The other cuts will come at the company's Linden, N.J., truck factory.</P>
<P>"These layoffs have really sent a ripple of concern through the workers in our plant," said Gene Austin, a delegate in United Auto Workers (UAW) Local 594 at the truck and bus plant in Pontiac, Mich.</P>
<P>Layoffs eliminated the evening shift in the Pontiac plant a year ago. But the work was farmed out to other GM plants across the country--with workers at those facilities forced to work more overtime, a direct violation of the union's contract.</P>
<P>Today, overtime has returned to the Pontiac plant amid strong truck sales. "The UAW said we didn't have a grievance," said Austin, who is part of a network of union activists known as the UAW Solidarity Coalition.</P>
<P>But union members pressed the issue. "The company admits it owes us 600 jobs, and we figure that it's 900," Austin said. "Now come the layoffs at Orion. It's a tit-for-tat situation."</P>
<P>GM's announcement comes a month after Ford Motor Co. said it would lay off 35,000 workers worldwide and shut down five plants.</P>
<P>The auto industry raked in record profits in the late 1990s. But with the recession in full swing, the auto bosses are getting ready to slash "excess capacity"--and that means workers will be asked to pay.</P>
<P>But UAW leaders have refused to abandon their attempt to build partnership with management. "It's going to take a union that will stand up for its members to fight this, and I don't know if we have that," Austin said. "The will of the union is at the heart of the matter."</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Don't close Waltham Hospital!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mark Cooper</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WALTHAM, Mass.--About 1,200 angry hospital workers and local citizens packed Waltham High School auditorium February 11 to voice their opposition to the proposed closing of Waltham Hospital.</P>
<P>The possible closing is a potential health care disaster in a heavily populated region that has already suffered from hospital closings. Closing the hospital will also mean the loss of some 1,200 jobs at a time when the economy is in a recession and the prospect of finding work is not good.</P>
<P>The decision to close Waltham Hospital follows a pattern in which supposedly nonprofit health care providers close hospitals in working-class communities due to the fact that Medicare and Medicaid don't bring in the big profits that private health care coverage does.</P>
<P>If CareGroup--the HMO that administers Waltham Hospital--gets its way, the 116-year-old hospital will close its doors April 11. Amid a chorus of boos, John Hamill--a chair at Sovereign Bank representing CareGroup--stated that the hospital was losing too much money and therefore needed to shut down.</P>
<P>"Nothing in medical school prepared me for talking about finances," responded Dr. William Mulroy. "And I find it absurd that I stand here prepared to talk to you about finances! But then again, we just heard from a chairman of a bank about running a hospital!"</P>
<P>Many at the meeting were angry at the silence of state elected officials--one man commented that it seemed more important for those in power to save the Red Sox than an important health care facility.</P>
<P>Many also felt that the special preference given to facilities that deal heavily with private health care was weakening the U.S. health care system--a system that was already ranked 37th in the world by the World Health Organization two years ago.</P>
<P>"The hospital was built by the hard work of Walthamites," said Waltham resident Patrick O'Brien at a similar meeting in late January. "It should not be sold to pay off the debts of others!"</P>
<P>"Abandoning health care to managed care and the so-called 'free market' is failing Waltham and is failing Massachusetts," said Peg O'Connor, a nurse at Waltham Hospital for the past 36 years.</P>

