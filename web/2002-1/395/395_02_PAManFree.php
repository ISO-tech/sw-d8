<P><font face="Times New Roman, Times, serif" size="4">Pa. man proven innocent</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Finally free after 15 years in prison</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 22, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"A LIVING hell." That's how Bruce Godschalk described the last 15 years of his life--years spent wrongfully imprisoned in Montgomery County, Pa.--when he finally walked free on February 14.</P>
<P>In 1987, Godschalk was convicted and sentenced to 10 to 20 years for two rapes. One victim was unable to identify him, and a second picked him out from a mug shot six months after the crime was committed.</P>
<P>A jury took just three hours to convict Godschalk, based mainly on a "confession" that police forced out of him after hours of interrogation without a lawyer present. Godschalk retracted the confession immediately afterward, but the court allowed his statement to be used to convict him.</P>
<P>"That was [the detective's] confession, not mine," Godschalk told reporters. "I was tricked. I was intimidated. He forced me to confess to it because he needed a confession for a conviction."</P>
<P>When advances in DNA analysis gave him hope that his name could be cleared, Godschalk begged again and again for testing. But for more than seven years, the Montgomery County District Attorney's office fought the requests. The tests weren't ordered until 1997.</P>
<P>It was only when Godschalk contacted the Innocence Project in 1997 that lawyers finally were able to get the tests ordered. The results showed that Godschalk wasn't the rapist.</P>
<P>But District Attorney Bruce Castor still refused to release him--and ordered a second lab to redo the tests. When results from the second lab confirmed that Godschalk couldn't possibly have committed the rapes, Castor was finally forced to allow him to be set free.</P>
<P>But not before taking a callous parting shot. "I am not convinced that Bruce Godschalk was innocent," Castor told reporters. "What I am convinced of is that he cannot be proven innocent beyond a reasonable doubt. And in this business, a tie goes to the defendant."</P>
<P>"This business" stole 15 years of Bruce Godschalk's life. While in prison, both of his parents and his sister died, and he lost touch with his former girlfriend. The satisfaction of seeing him proven innocent won't ever make up for the life that Godschalk lost.</P>

