<P><font face="Times New Roman, Times, serif" size="4">The Enron scandal</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Tip of the iceberg</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 22, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ENRON ISN'T the half of it. Revelations of more colossal financial scams in Corporate America swept into the news last week amid the bankruptcy of telecommunications company Global Crossing.</P>
<P>As in Enron's case, company executives cashed in while workers got stiffed. Communications Workers of America Local 1170 President Linda McGrath said that many workers put Global Crossing stock into their 401(k) plans--which couldn't be sold for five years. And just like Enron, Global Crossing got away with its scams by paying off politicians of both parties.</P>
<P>But while Enron shoveled more cash to Republicans, Global Crossing favored Democrats. For example, Terry McAuliffe, Bill Clinton's former adviser and now head of the Democratic National Committee, turned a $100,000 investment in Global Crossing into a stock sale that netted $18 million.</P>
<P>Headquartered in Beverly Hills but officially based in Bermuda in order to dodge taxes, Global Crossing shot to prominence in the late 1990s with its promise to wire the world with a $15 billion fiber optic network.</P>
<P>Problem was, plenty of other companies had the same idea--so there was a glut in fiber optic capacity. Global Crossing CEO Gary Winnick saw trouble coming and decided to sell his stock--pocketing $734 million.</P>
<P>Meanwhile, the company kept inflating its profits by selling access on its network to other phone companies like Qwest--and then buying back access on other networks for the same amount of money. No money ever changed hands--but each company listed the "sales" as income anyway to hoodwink investors.</P>
<P>Last year, Global Crossing recorded $150 million in cash revenue from such deals, even though it never got a dime. Telecom companies everywhere did exactly the same thing. "You can't fake cash flow," wrote <I>New York Times</I> columnist Floyd Norris, quoting an old business saying. "Well, actually, you can. Now we know that accountants rose to the challenge. If investors wanted to see operating cash flow, well, by jiggery, they would see it."</P>
<P>In their hearings into the Enron scandal, members of Congress tried to focus on people like Winnick and former Enron bosses Jeffrey Skilling and Ken Lay, along with crooked accountants at Arthur Andersen--which worked for Global Crossing and Qwest as well as Enron.</P>
<P>But these politicians are terrified of exposing the cesspool of political payoffs and conflicts of interests that link big business and their front men in government. "Partisans are leery of digging too deeply lest they get themselves dirty," <I>Business Week</I> reported. And the staggering scale of corporate rip-offs goes far beyond the handful of crooks exposed so far.</P>
<P>Consider the investment bankers at Citigroup. Not only did they help Enron design partnerships to hide the company's huge debts, they sold other clients on ways to remove "certain items from 'plain view,' thus enhancing the appearance of balance sheet," as the bank tactfully put it. Citigroup also disguised a $2.4 billion loan to Enron beginning in 1999. That kept Enron's stock price going up--and helped Citibank haul in millions more in fees.</P>
<P>There's no telling yet exactly what these scandals will turn up next. But the lessons are already clear. This system is geared to reward corporate hustlers, crooks and thieves. And it's working people who pay the price.</P>

