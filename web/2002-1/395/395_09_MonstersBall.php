<P><font face="Times New Roman, Times, serif" size="5"><b>A monster called the death penalty</b></font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><P><B>MOVIES:</B> <B><I>Monster's Ball,</B></I> directed by Marc Forster, written by Milo Addica and Will Rokos, starring Halle Berry and Billy Bob Thornton.</P></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Helen Redmond</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 22, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><I><P>MONSTER'S BALL</I> is a monster of a movie. It has politics, power and passion--a rarity among films today. At its core, the movie is about racism and alienation so profound that it leads to state-sponsored killing--to the death penalty, suicide and homicide.</P>
<P>The film is set in the Deep South. Hank Grotowski (Billy Bob Thornton) stands in the middle of three generations of prison guards. He cares for his retired father--a vile, woman-hating, unrepentant racist (Peter Boyle).</P>
<P>Hank, who is also a racist, is his son Sonny's superior officer on death row. Together, Hank and Sonny supervise the execution of Lawrence Musgrove, played by hip-hop artist Sean Combs.</P>
<P>These scenes are among the most gut-wrenching, as they show exactly how an execution by electrocution is prepared and carried out.</P>
<P>Just hours before Musgrove dies, he draws portraits of Hank and Sonny. They give him his last meal and sit outside his cell watching him until it is time to go. There is little dialogue during this part of the film, but the silence, cinematography and music speak volumes.</P>
<P>The execution deeply affects both men. While walking Mugrove to the death chamber, Sonny wavers and runs to the bathroom to vomit. Hank decides to retire early.</P>
<I><P>Monster's Ball</I> exposes the death penalty's barbarity and the toll it takes on the people who carry it out.</P>
<P>Leticia Musgrove (Halle Berry) is the executed man's wife. As a single mother with no social support, she struggles to pay the bills and take care of her morbidly obese son. She loves her son and yet, due to circumstances, is driven to verbally and physically abuse him.</P>
<P>After striking her son for eating a candy bar, she holds him and cries.</P>
<P>Leticia and Hank meet at the diner where she works. When Leticia's son is hit by a car, Hank, who is driving by, stops and takes them to the hospital. The hit and run accident sets up their relationship. Hank's role in her husband's execution remains a dark secret at the center of the film.</P>
<P>The strength of the writing is that nothing is quite what it seems and the characters are complex and all too human. But the essential message of the film is that people can and do change--a corrective to all the tragedy, racism and violence the film depicts.</P>
<P>Berry's and Thornton's acting is astonishing as they express anguish, loneliness, confusion and rage at a world that has beaten them down.</P>
<P>Go see <I>Monster's Ball</I> now--you won't be able to get it out of your head for days after.</P>

