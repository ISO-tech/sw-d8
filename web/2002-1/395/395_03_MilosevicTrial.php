<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Old friend of the U.S. government on trial for war crimes</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Hypocrisy in The Hague</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 22, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. government loves to hate Slobodan Milosevic. In 1999, the U.S. portrayed the Serb leader as the "Hitler of the Balkans" in order to build support for the NATO war over Kosovo.</P>
<P>A massive bombing campaign demolished Milosevic's military, and NATO took over Kosovo, where peacekeepers stood by as ethnic Albanians committed ethnic cleansing in reverse against Serbs.</P>
<P>After Milosevic was toppled by a mass revolt, the U.S. strong-armed Yugoslavia's new government into turning over Milosevic to the UN International Criminal Tribunal for the Former Yugoslavia--by threatening to withhold desperately needed economic aid.</P>
<P>Milosevic's trial at The Hague is entering its second week, and Washington is leading the outcry against this "enemy of democracy." But it wasn't always that way. In fact, for much of the 1990s, the U.S. government supported Milosevic.</P>
<P>In the early 1990s, none other than George Bush Sr. looked to Milosevic to impose Serbian domination as the former Yugoslavia broke up. But this strategy failed, and Yugoslavia descended into a civil war, centered in Bosnia, between warring nationalist factions of Serbs, Croats and Muslims.</P>
<P>Leaders of all sides committed atrocities, while ordinary people suffered persecution--from other ethnic groups, but also from their own nationalist leaders if they failed to join the campaign of hatred against rivals.</P>
<P>The Serbs were the best-armed side in the Balkans wars. Yet the biggest single episode of ethnic cleansing was carried out by Croatia, which forced 200,000 Serbs to flee their homes in the Krajina region--after getting the go-ahead from the U.S.</P>
<P>To put an end to the Bosnian war, U.S. negotiators came up with the Dayton accords--and looked to Milosevic to enforce the "peace." In return, the U.S. turned a blind eye to Serbia's oppression of ethnic Albanians in Kosovo.</P>
<P>But a few years later, when the conflict in Kosovo threatened to uncork a wider regional war, the U.S. turned on its old friend. Milosevic was demonized--just like former U.S. allies Saddam Hussein and Manuel Noriega before him.</P>
<P>To be sure, Milosevic is responsible for war crimes. But NATO's bombing of the Balkans was a war crime itself--killing Serb civilians and ethnic Albanian refugees alike; destroying power grids that supplied electricity to hospitals, schools and homes; and littering the country with cancer-causing depleted uranium shell fragments.</P>
<P>Yet Bill Clinton will never be tried. Neither will former Secretary of State Madeleine Albright or former UN Ambassador Richard Holbrooke, the chief architect of the Dayton accords.</P>
<P>The U.S. government's complaints about Milosevic are sheer hypocrisy. Like a long line of others, Milosevic is a butcher that the U.S. wooed and coddled--until he stepped out of line and Washington painted him as the villain.</P>
<P>Meanwhile, the worst war criminals of all--the ones in power in Washington--are getting away with mass murder.</P>

