<P><font face="Times New Roman, Times, serif" size="5"><b>Organizing for justice in Palestine</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Todd Chretien</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 22, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ISRAEL'S SAVAGE crackdown against Palestinians has provoked an activist response in the U.S. With continued Israeli air strikes and invasions of territory controlled by the Palestinian Authority serving as the backdrop, some 400 students and Palestine solidarity activists attended the Students for Justice in Palestine (SJP) conference at the University of California-Berkeley.</P>
<P>Activists came from as far away as Texas, New York, Montreal and the Occupied Territories to participate in three days of discussion and organizing. More than 25 campuses were represented at the conference, which laid out a plan of action for activists.</P>
<P>The weekend drew many prominent activists and authors from the Palestine solidarity movement, including As'ad Abu Khalil, Phyllis Bennis, Ghassan Andoni and Altaf Husain. At more than 30 workshops, attendees discussed everything from the nuts and bolts of organizing direct action, to the history of the Palestinian student movement, to how to develop links between religious and secular student organizations.</P>
<P>Almost 300 people packed the final plenary session, which featured Elias Rashmawi from the American-Arab Anti-Discrimination Committee, Norman Solomon from Fairness and Accuracy in Reporting and Anthony Arnove from the International Socialist Organization.</P>
<P>Upcoming SJP activities will center around building a campaign to force U.S. universities to divest from companies that do business in Israel. This campaign is modeled on the South African divestment movement of the 1980s, which contributed to the downfall of apartheid.</P>
<P>The anniversary of the 1948 massacre of Palestinians at Deir Yassin on April 9 will be marked by a national "Divestment Day" aimed at raising awareness and organization. Regional conferences in late September will mark the second anniversary of the new Intifada and coordinate activities. Lastly, a Divestment Research Center will begin producing materials about the links between U.S. schools and Israel.</P>
<I><P>For information about the conference or to start an SJP chapter on your campus, go to www.justiceinpalestine.org on the Web.</P>
