<P><font face="Times New Roman, Times, serif" size="4"><b>Coming to a store near you?</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. company CMG Worldwide recently purchased exclusive rights to the image and brand name of "Mahatma Gandhi." The leader of India's fight for independence from the West now joins the ranks of Marilyn Monroe, Princess Di and James Dean on CMG's roster of celebrities.</P>
<P>The deal was struck by Gandhi's great-grandson, Tushar Gandhi, the managing trustee of the Gandhi Foundation. The firm has already helped Tushar earn approximately $60,000 from an ad agency that wants to use the Mahatma's image for promoting an unspecified product.</P>
<P>Proceeds from the commercial use of Mahatma's image will go to the Gandhi Foundation. Tushar Gandhi was apparently convinced to strike the deal with CMG when they called and warned him about the "possible misuse" of Mahatma's name and image.</P>
<P>"I do not want his name or image to be used irreverently, such as on a meat product or on lingerie," Tushar said.</P>
<P>--<I>Tribune of India,</I> February 8, 2002</P>

