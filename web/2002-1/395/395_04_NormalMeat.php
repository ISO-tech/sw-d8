<p><font face="Arial, Helvetica, sans-serif" size="2">February 22, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>EVEN OLYMPIC athletes can't get a decent meal. Athletes from Belarus, the former USSR republic, have filed a formal complaint with U.S. Olympic organizers for not providing enough food and feeding them McDonald's hamburgers.</P>
<P>"The biathletes have not received dinner for three days," said Ivan Ryzhenkov, the captain of Belarus' Olympic team. "We've had trouble with food from the first day."</P>
<P>Even worse, say the athletes, when food does come, it's usually from McDonald's. "Our sportsmen are getting sandwiches from McDonald's and various hamburgers," said Alexander Grigorov, the Belarus Deputy Sports Minister. "But they need normal meat, fresh juice, hot soup."</P>
