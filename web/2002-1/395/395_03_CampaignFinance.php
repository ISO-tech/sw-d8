<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Congress tinkers with the rules for buying influence</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Reform without real change</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 22, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"KENNY BOY was a real help." That's what Sen. John McCain (R-Ariz.) said as the House of Representatives voted 240 to 189 last week in favor of a new campaign finance reform law.</P>
<P>The proposal was bottled up last year by House Republican leaders, acting on orders from the Bush White House. But growing disgust with former Enron CEO Ken "Kenny Boy" Lay and his success at buying influence throughout Washington forced the issue back onto the table.</P>
<P>The centerpiece of the Shays-Meehan bill--like the companion legislation sponsored by McCain and Sen. Russ Feingold (D-Wis.) that passed the Senate last year--is a ban on "soft money" donations to political parties.</P>
<P>"Soft money" allows corporations like Enron to funnel unlimited sums into Washington--as long as they gave the money to parties, not to particular candidates. So over the past decade, Enron gave a total of $3.9 million in soft money to Republicans and Democrats--compared to $1 million directly to federal candidates.</P>
<P>But there are giant loopholes in the new legislation. State and local parties would still be allowed to raise soft money in amounts of $10,000 or less. "At the end of the day, whoever wins the presidential nomination will have a list of several hundred people who have traditionally given $100,000 or more," Robert Farmer, a Democratic fundraiser, told the <I>New York Times</I>. "They'll get the list and call those people. They'll say, 'You can't give $100,000 now, but you are permitted to give money to parties in these 10 states where we have the biggest problem.'"</P>
<P>Plus, the legislation doubles the limit on "hard money" contributions to specific politicians from $1,000 to $2,000. Corporate America will have no problem tapping executives and bundling big bucks to give to their favorite candidates.</P>
<P>No wonder lobbyists seem so unconcerned about the House vote. They've figured out how to work the new system, even before it's implemented. "The money is going to squirt out somewhere," said Bruce Cain, director of the Institute of Governmental Studies at University of California-Berkeley. "All the reform is doing is re-channeling where the flow is."</P>
<P>The Shays-Meehan bill will now go to the Senate for a vote, where Republicans are threatening a filibuster. And George W. Bush could still veto the legislation. But even if campaign finance reform is signed into law, the system of legalized bribery that drives Washington will go on. After all, "soft money" contributions were the centerpiece of the last effort to "reform" the campaign finance system.</P>
<P>The key to changing Washington isn't tinkering with loopholes in the system, but building a real challenge to the two mainstream parties that serve Corporate America through thick and thin.</P>
<P>Ralph Nader's 2000 presidential campaign did much more to mobilize an opposition to business as usual in Washington than all the reform plans to turn off one faucet of corporate bribe money--only to turn on another.</P>
<P>The Washington system is permanently rigged in favor of the bosses. We need to build an alternative based on real democracy.</P>

