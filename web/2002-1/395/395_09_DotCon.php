<P><font face="Times New Roman, Times, serif" size="5"><b>Dot.conning their way to the top</b></font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><P><B>BOOKS:</B> John Cassidy, <B><I>Dot.con: The Greatest Story Ever Sold.</B></I> HarperCollins, 2002, $25.95.</P></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Scott Johnson</b><font face="Arial, Helvetica, sans-serif" size="2"> | February 22, 2002 | Page 9</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"WE'RE GOING to put the fun back into funerals." This is a quote from a budding entrepreneur at Funerals.com, just one of many Internet companies lambasted by journalist John Cassidy in his book <I>Dot.con</I>.</P>
<P>Written as a brief history of the even briefer period when the Internet boom was praised as the future of capitalism, Cassidy joyously documents the ludicrous business models, scheming and corruption behind the scenes.</P>
<P>One of Cassidy's main targets is the Initial Public Offering (IPO) of Internet stocks, where companies sold their shares to the public and saw their market value skyrocket. In the case of middleman airline ticket agents like Priceline.com, stocks were valued at more than the entire airline industry after their IPOs--without ever turning a profit.</P>
<P>Cassidy discusses how the entire financial industry encouraged and profited from this speculative boom. For example, Merrill Lynch stated in 1998 that online trading was "a serious threat to Americans' financial lives." A year later, seeing their competitors profit, Merrill Lynch allowed customers to trade stocks online.</P>
<P>Cassidy also shows how companies like Amazon.com sold only a tiny fraction of their stock on the market. The rest was privately held by CEO Jeff Bezos and other executives. By creating an artificial shortage, they kept prices high. Had Bezos and others sold their stock to the public, Cassidy notes, the price would have plummeted, blowing a hole through the Internet bubble.</P>
<P>Unfortunately, the little analysis Cassidy provides is weak, as he explains the bubble as the product of a "herd behavior." Cassidy shies away from shedding light on the deeper problems.</P>
<P>However, as the Enron scandal unravels, <I>Dot.con</I> is an entertaining look at how the financial world lies, cheats and steals its way to the top.</P>

