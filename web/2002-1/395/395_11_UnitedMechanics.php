<P><font face="Times New Roman, Times, serif" size="4">Mechanics to vote on tentative agreement</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>New showdown at United</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 22, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>A TENTATIVE agreement between mechanics and United Airlines this week averted a strike but may only delay a confrontation.</P>
<P>The deal, reached as <I>Socialist Worker</I> went to press, reportedly modifies the so-called "linkage letter" that would have granted the mechanics a wage raise today but would lead to concessions in only a few months.</P>
<P>The letter was created by the Presidential Emergency Board (PEB), a government authority appointed by George W. Bush to oversee negotiations. The PEB accepted the union's demand for a 37 percent pay raise, since the mechanics haven't had a pay increase since 1994. But its proposed settlement included a clause that would have forced mechanics, members of International Association of Machinists (IAM) District 141M, to accept wage cuts if the other unions at United agree to them.</P>
<P>United management signed on to the PEB's plan. But mechanics were furious at the idea that their long-overdue raises could quickly be erased.</P>
<P>After rejecting management's initial offer in a 99 percent strike vote, they rejected the PEB's proposal last week with 86 percent again voting to strike. Now a new vote will be scheduled by early March.</P>
<P>Yet the main difference in the new proposal is that mechanics will have the right to vote on management's concessions. While that sounds democratic, the complications of the National Railway Labor Act that governs the airlines means that it will be difficult to take job actions in the future.</P>
<P>That suits top IAM leaders, who convinced members to accept an employee stock ownership plan in exchange for wage cuts and other concessions in 1994. They have talked tough but are trying to avoid a confrontation. But rank-and-file mechanics have had it with givebacks.</P>
<P>Although the company is losing money today, it made record profits in the 1990s--and workers got nothing. Even though the 37 percent wage increase sounds enormous, mechanics will still be making <I>less</I> in real dollar terms than they did in 1994.</P>
<P>"People voted to strike the first time around because they don't want to take concessions--and voted a second time against the PEB's proposal for the same reason," said Jennifer Biddle, a member of IAM Local Lodge 1781, who is organizing for a "no" vote. "We can't let the company or the international wear us down. We can't give in to the pressure of time. Too much can be lost. If we lose this it will have an effect on workers far beyond UAL and the aviation industry. The question is--who is going to step up to fight?"</P>

