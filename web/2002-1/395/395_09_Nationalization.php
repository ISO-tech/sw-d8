<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Socialism is about workers' control</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 22, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>FOR MANY people, socialism is popularly defined as the degree of state intervention in the economy. Societies with strong social spending programs and some state-run industries were considered moderately socialist--like Sweden. Societies with total state control of the economy--like Russia before 1989--were fully socialist, or "communist."</P>
<P>The existence of a more or less expansive social safety net may indicate a strong history of class struggle, but concessions wrested from a capitalist state do not render that society socialist.</P>
<P>History shows that reform socialists who have sought public office have ended up not transforming society, but themselves being transformed into capitalism's left flank of defense.</P>
<P>French socialists Fran&ccedil;ois Mitterand in the 1980s and Lionel Jospin in the 1990s implemented policies that have helped to gut France's social safety net--and saved the French bosses' system to stay profitable.</P>
<P>The degree of state ownership likewise is not a good measure of socialism. State-run industries under capitalism do not operate as islands of socialism, but on the same principles of profit and loss as the private sector.</P>
<P>As Frederick Engels wrote more than 100 years ago: "A kind of spurious socialism has arisen&#133;that&#133;declares <I>all</I> state ownership&#133;to be socialistic."</P>
<P>"Certainly," he continues, "if the taking over by the state of the tobacco industry is socialistic, then Napoleon&#133;must be numbered among the founders of socialism."</P>
<P>Even total state ownership, Engels argued, is not socialism. "The transformation&#133;into state ownership, does not do away with the capitalistic nature of the productive forces," he wrote. "The modern state, no matter what its form, is essentially a capitalistic machine, the state of the capitalists&#133;The more it proceeds to take over productive forces, the more does it actually become the national capitalist, the more citizens does it exploit."</P>
<P>Workers remain exploited in the same way as before, only their employer becomes not General Motors or Ford, but the state. "The capitalist relation is not done away with," writes Engels, "it is rather brought to a head."</P>
<P>The question of nationalization is not unimportant. But so long as nationalization is based upon production and competition on a world market, it will be <I>capitalist</I> nationalization.</P>
<P>Hence Stalin's Russia--where workers had ceased to have any control over society, where the state became stronger and more repressive, and where the goal was to create a competitive military machine--had nothing to do with socialism.</P>
<P>For Marx, nationalization could only be a weapon in the transformation of society in a socialist direction because it represented the expropriation of society's productive forces by the working class. Socialism for Marx was the collective seizure of political power by the associated producers--the "self-emancipation of the working class."</P>
<P>The question of whether socialism exists therefore does not depend on this or that form of property (private ownership or nationalization), but on whether the society is in the hands of the associated producers--the working class.</P>
<P>In fact, the aim of workers' power is to implement a series of economic and social transformations that does away with all class distinctions and creates a society whereby the state--an instrument of class domination--gradually fades away.</P>
<P>As Engels wrote, "The proletariat seizes the public power, and by means of this transforms the socialized means of production, slipping from the hands of the bourgeoisie, into public property."</P>
<P>The economy, now under workers' control, socializes the means of production so that production and distribution can be carried out according to a rational plan that meets human need. "In proportion as anarchy in social production vanishes," Engels continues, "the political authority of the state dies out."</P>

