<P><font face="Times New Roman, Times, serif" size="4">Israel demands obedience from Palestinians</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Sowing seeds of violence</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 22, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Recently, Palestinian leader Yasser Arafat has been demonized by American and Israeli politicians to no end. They say he has "enhanced terrorism," failed to "reign in militants" and is returning to "terrorist ways."</P>
<P>The source of this vitriolic abuse has nothing to do with honesty. It has everything to do with an underlying American-Israeli attitude that Palestinian natives should kneel to their colonial "superiors."</P>
<P>The very basis of this conflict lies with Israel's forcible expulsion of 800,000 Palestinians and the massacre of hundreds during the establishment of the Israeli state in 1948. Since then, Israel has formally occupied all Palestinian land with military force, expropriated countless thousands in the name of expansion, condemned millions to refugee camps and suppressed the rest with brute military force.</P>
<P>Ariel Sharon's recent actions illustrate this all too well. During a December cease-fire, not one Israeli civilian was killed for three weeks. In the same period, 28 Palestinians, including 11 children, were slaughtered by the Israel Defense Forces (IDF). In early January, the IDF demolished more than 60 homes in a Palestinian refugee camp, leaving 600 civilians homeless.</P>
<P>For now, Israel pretends that it can increase the viciousness of the occupation and simultaneously demand Palestinian obedience. Its anger with Arafat is due to the fact that, as a colonial puppet, he can no longer help them silence and subdue an entire people.</P>
<P>Like a self-appointed farmer, Israel wants to separate "the wheat from the chaff"--the chaff being any resistance to their brutality. But farmers will reap what they sow, and this farmer--fond not of ploughs and harvesters, but of tanks and bulldozers--has sown the seeds of violence and hate.</P>
<B><P>M. Junaid Alam,</B> Boston</P>

