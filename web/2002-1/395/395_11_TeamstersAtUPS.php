<P><font face="Times New Roman, Times, serif" size="5"><b>UPS and Teamsters begin talks on new contract</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Donny Schraffenberger, steward, Teamster Local 705</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 22, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>UNITED PARCEL Service (UPS) and the Teamsters opened negotiations last month in advance of an August 1 deadline.</P>
<P>The last time the two squared off, UPS got battered in a 15-day national strike in 1997 that won the hearts and minds of two out of every three Americans. Ten thousand new full-time jobs eventually had to be created, putting teeth into the strike slogan "Part-time America doesn't work."</P>
<P>Although UPS tried to nullify the full-time job guarantee, a national arbitrator eventually forced management to create them. Wages did rise to $8.50 an hour from $8 for starting part-timers, yet they are still a far cry from the $12 an hour that part-timers used to earn in the early 1980s. UPS has made record profits these last five years off the backs--literally--of its underpaid, part-time loaders and unloaders.</P>
<P>UPS management would like to avoid the bad publicity it received from the 1997 strike. Many longtime Teamster activists at UPS believe that management and Teamsters President James P. Hoffa will use the recession as an excuse to reach an early settlement and avoid a strike.</P>
<P>But the company has the money to meet workers' demands. UPS is the world's largest package delivery company, handling 3 billion packages per year and employing 370,000 people worldwide.</P>
<P>The Teamsters are asking for 3,000 new full-time jobs a year and a three-year contract. We also want stronger contract language to stop supervisors from doing union work--a real problem at most UPS facilities.</P>
<P>Many part-time workers get robbed of their measly three-and-a-half-hour shift guarantee when supervisors sort and move packages. At the same time, drivers work excessive forced overtime because UPS management wants to limit full-time benefit expenses by using as few drivers as possible.</P>
<P>"It is the 210,000 Teamsters that have made billions for UPS," said Hoffa at a January 30 press conference. "It is time they get a share of the profits earned since the last contract."</P>
<P>Although many of Hoffa's initial contract proposals look good, the question is whether his contract negotiating team--made up of union officials and no rank-and-file members--will really hold the line. If the recent history of Hoffa's negotiations for workers at Anheuser-Busch and for flight attendants at Northwest Airlines is any indication, the answer is no.</P>
<P>UPS hasn't made public its contract proposals, and Hoffa still hasn't revealed specific Teamster wage demands. And in Chicago, management has already begun laying off feeder (over-the-road) drivers supposedly because of the recession.</P>
<P>One thing is clear: Rank-and-file Teamsters will have to organize at work to press management for their demands and push Hoffa into delivering on his promises.</P>

