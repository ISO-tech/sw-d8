<P><font face="Times New Roman, Times, serif" size="5"><b>Support the socialists in Zimbabwe</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 22, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ZIMBABWEAN PRESIDENT Robert Mugabe's crackdown on political opposition took aim at socialists and union activists last week.</P>
<P>On February 15, police armed with tear gas and clubs attacked a march of hundreds demanding "no to dictatorship, no to neoliberalism and poverty." The demonstrators' "crime" was to protest the Public Order and Security Act, which prohibits virtually all dissent.</P>
<P>Among those arrested was Munyardze Gwisai, a representative in Zimbabwe's parliament and a leading member of the International Socialist Organization-Zimbabwe (ISO-Z).</P>
<P>The ISO-Z has bitterly opposed the cynical way that Mugabe has used land reform and his past as an anticolonial freedom fighter to justify suppression of democratic and labor rights. Mugabe's ZANU-PF party has responded with beatings and arrests of ISO members simply for selling their newspaper, which is also called <I>Socialist Worker</I>.</P>
<P>The 15 arrested at the march are out on bail, but will go before a judge on March 4 for arraignment. In the U.S., solidarity protests will be held at Zimbabwean diplomatic offices in Washington, D.C., and New York on March 1.</P>
<P>The ISO-Z has requested international support for an emergency fighting fund--and our readers have already responded generously. But more funds are urgently needed. Please contribute to this crucial struggle for independent working-class politics in southern Africa.</P>
