<font face="Times New Roman, Times, serif" size="5"><b>Leading Palestinian activist arrested in NYC</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Wengraf</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 3, 2002 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">FEDERAL OFFICIALS stepped up their attack on civil liberties last week with threats to deport a well-known Palestinian activist in New York City. </P>
<P>Faruk Abdel-Muhti was arrested early April 26 at his home in Queens. Three New York cops and an Immigration and Naturalization Service (INS) agent showed up at his home, banged on the door and threatened to break it down--claiming there were explosives and weapons in the apartment. Once inside, they changed their story, telling Abdel-Muhti that he was under arrest for being in the U.S. illegally. </P>
<P>A member of the Palestine Aid Society and a regular guest on the left-wing radio station WBAI, Abdel-Muhti has been an outspoken opponent of Israel's terror against Palestinians. Now he's being targeted for it.</P>
<P>Abdel-Muhti's lawyer, Gilma Camargo, said at a press conference and protest outside the Federal Building that the INS agent in the raid "only asked him political questions about his work here in the Palestinian community." Camargo said that the government couldn't even produce an order of deportation, which it claims dates from 1995. </P>
<P>"His only crime was the crime of speaking out for the right of self-determination for Palestinians," Camargo told <I>Socialist Worker. </I>"They think they can just deport people they don't like. They think he could just be disappeared like so many others, but we won't allow it."</P>
<P>Activists who have been fighting the Justice Department's witch-hunt of Arabs and Muslims in its September 11 investigation have taken up Abdel-Muhti's case. "Faruk is one of hundreds, thousands, who have been disappeared," said Subhash Kateel of Desis Rising Up and Moving, which has organized protests to pressure the INS. "His arrest highlights our demand: we need answers by the INS and the FBI."</P>
<P><I>Call the INS's district director for New Jersey, Andrea Quarantillo, at 973-645-4421 and demand the immediate release of Faruk Abdel-Muhti.</I> </P>

