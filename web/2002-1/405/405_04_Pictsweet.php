<font face="Times New Roman, Times, serif" size="5"><b>Workers fight dirty tricks at Pictsweet</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 3, 2002 | Page 4</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><B>Dear <I>Socialist Worker,</I></B></P>
<P>I'm writing to let your readers know about a nationwide boycott being conducted by the United Farm Workers of America (UFW). Workers at the Pictsweet Mushroom Farms site in Ventura, Calif., are suffering horribly at the hands of management. </P>
<P>Since 1987, when United Foods bought out the company's owners, Pictsweet management has refused even to negotiate with workers. These heartless, profit-obsessed bosses have kept wages low and offered an expensive and inadequate medical plan. And they've tried dirty trick after dirty trick in an effort to bust the union. </P>
<P>One strategy has been an attempt to decertify the UFW's right to represent workers. The UFW also reports that pro-union workers have faced threats, harassment, wage cuts, cuts in hours and firings. </P>
<P>Pictsweet managers have also created startlingly dangerous working conditions. On March 9, 2001, a three-acre compost heap spontaneously ignited, sending toxic fumes into the air for nine straight days. The county warned residents to stay indoors, yet Pictsweet workers were forced to work in smoke-filled rooms. "My throat and eyes hurt very much, and I had difficulty breathing, and I constantly choked from the smoke...and the company did nothing," employee Lilia Orozco told the UFW. </P>
<P>As part of its fight against these outrages, the UFW has launched a nationwide boycott of all Pictsweet mushrooms and frozen vegetables. As socialists, we should stand in solidarity with the courageous Pictsweet workers, who refuse to give in under these harshest of conditions. </P>
<P><B>Jennifer Rowe, </B>Washington, D.C. </P>
<P><I>To learn more, visit www.unitedfarmworkers.com.</I> </P>

