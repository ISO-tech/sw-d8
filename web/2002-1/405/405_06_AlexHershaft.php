<font face="Times New Roman, Times, serif" size="3"><b>ALEX HERSHAFT</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A Warsaw Ghetto survivor speaks out</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 3, 2002 | Page 6</font></p>

<P><font face="Times New Roman, Times, serif" size="3">ALEX HERSHAFT is a survivor of the Warsaw Ghetto, where the German Nazis went on a rampage of terror against Jews during the Second World War. As he wrote in a recent letter to the <I>Washington Post, </I>the devastation carried out by Israel today in the West Bank reminds him of his childhood years in the ghetto. Hershaft talked to <I>Socialist Worker </I>about his horror at Israel's massacre in Jenin.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<P><font face="Times New Roman, Times, serif" size="3">THERE'S A low regard for human life. The cavalier demolition of housing, with people inside possibly being buried under the rubble--the visual image of what happened--brought back memories of the Warsaw Ghetto.</P>
<P>After the war ended, we all said in unison, "Never again!" And here it is we, the Jews, who are doing it to the Arabs. It's ironic to me. It shows the banality of evil--that it's not endemic to any one people. It was so easy to blame the Germans, but the Americans did it in Vietnam, and now the Jews are doing it in Palestine, and it seems there's no monopoly on evil.</P>
<P>I participated in the Palestinian solidarity march on April 20 in Washington, D.C. I thought it was comforting. There were some signs and chants that made me uncomfortable, but most of it was very peaceful and amazingly devoid of hatred and rancor.</P>
<P>I consider myself an Israeli patriot. I realize they have a terrible problem with the suicide bombings, but they're just creating a bigger problem. How can you end suicide bombing by more bombing?</P>
<P>There was one sign on the march that I thought really struck a chord. It said the difference between a terrorist and a freedom fighter is an F-16--the ability to drop bombs rather than having to carry them.</P>

