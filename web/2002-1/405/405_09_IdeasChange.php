<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>How do workers' ideas change?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 3, 2002 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE FRENCH essayist Michel de Montaigne wrote in 1575 about the visit of three American Indians to France to meet Charles IX. The Indians, who knew no class division in their own society, saw that French society was divided into very unequal "halves."</P>
<P>They noticed, wrote Montaigne, that "there were among us men full and crammed with all manner of commodities, while, in the meantime, their halves were begging at their doors, lean, and half-starved with hunger and poverty; and they thought it strange that these necessitous halves...did not take the others by the throats, or set fire to their houses."</P>
<P>Transported to the 21st century, the Indians might have the same question. The richest fifth of Americans have nine times more wealth than the poorest fifth. The wealthiest 1 percent of U.S. society has 38 percent of the nation's wealth. Why don't the vast majority, whose labor makes such obscene wealth possible, take the 1 percent "by the throat, or set fire to their houses"?</P>
<P>A number of factors prevent this. One is inertia--the inherent conservatism built into a set of human social relations once they are established. "Society does not change its institutions as need arises, the way a mechanic changes his instruments," wrote Leon Trotsky, a leading figure in the Russian Revolution of 1917. "On the contrary, society actually takes the institutions which hang upon it as given once for all."</P>
<P>This is reinforced at every turn by the mass media and the education system--all of which are controlled by corporations and the state. A set of false ideas--what Marx called ideology--is continually put forward that sanctions capitalism as the best and only possible form of society. Marx summed up this point in the <I>Communist Manifesto </I>by saying that "the ruling ideas of society are those of the ruling class."</P>
<P>The state is well prepared to resort to legal repression and even armed force if their rule is threatened. But for much of the time, force isn't necessary to maintain order. So long as a majority of people accept, to one degree or another, the ruling ideas, then direct coercion isn't necessary.</P>
<P>But if the dominant ideas are those of the rulers, how do people develop different ideas? The answer lies in the way workers' own experience--especially during struggle--clashes with the ruling ideas.</P>
<P>Many workers, for example, were angered by the fact that Bush's post-September 11 rescue packages were directed at helping companies rebound, but not workers. One of the conditions for the multibillion-dollar bailout package for United Airlines was that it find ways to cut labor costs--not rehire laid-off workers. The contradiction between the way the government treated corporations and workers in this crisis no doubt prompted some workers to begin to question the system's priorities.</P>
<P>If bosses hurt workers here (while chanting "united we stand"), then what kind of policies do they pursue abroad in our name? They can find the money to pay for bombs, but not for funding schools.</P>
<P>Protests and ultimately revolutions happen precisely because of the conservative inertia built into human social relations. These contradictions produce a buildup of anger and bitterness as people accept the humiliation and hardship of capitalism's crises.</P>
<P>"The swift changes of mass views and moods in an epoch of revolution...derive," writes Trotsky, "not from the flexibility and mobility of man's mind, but just the opposite, from its deep conservatism. The chronic lag of ideas and relations behind new objective conditions, right up to the moment when the latter crash over people in the form of a catastrophe," writes Trotsky, "is what creates in a period of revolution that leaping movement of ideas and passions which seems to the police mind a mere result of the activities of 'demagogues.'"</P>

