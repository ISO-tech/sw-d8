<p><font face="Times New Roman, Times, serif" size="4"><b>Fight for Palestinian rights</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Naveen Jaganathan, Julia Swaters and Mark Sweeney</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">AS GEORGE W. Bush and his cronies continued to proclaim that the U.S. "stands with Israel," hundreds of ordinary people across the country turned out to show their opposition to Israel's brutal war against the Palestinians.</P>
<P>On April 25, organizers at the University of Iowa pulled together a counterdemonstration against a rally for Israel. Sixty people turned out on less than a day's notice, making the counterdemonstration as large as the pro-Israel rally. When pro-Israeli speakers moved into the counterdemonstration and began heckling activists, supporters of Palestinian rights wouldn't be silenced. They raised chants of "Bush buys the bullets, Sharon pulls the trigger" and argued that Palestinians have a right to defend themselves against Israeli aggression.</P>
<P>"In this country, we've all been raised to believe that Israel is 'the good guy,'" one protester told <I>Socialist Worker. </I>"It's taken me a long time to learn differently and even longer to come out here and protest.  But finally it gets so bad that you just have to."</P>
<P>On the same night, former Israeli Prime Minister Ehud Barak&#150;an architect of Israeli brutality&#150;was on hand at Southern Connecticut State University lecturing on "Peace of the Brave: Prospects for the Middle East." What a sick joke! In his lecture, Barak described his program of building walls around Palestinian towns and defended the recent Israeli massacres of Palestinian civilians. Some 150 protesters turned out in the pouring rain to expose Barak as a fraud whose hands are stained with the blood of Palestinians. </P>
<P>Two days later in San Francisco, nearly 500 people marched in support of Palestinians and in protest against the latest atrocities being committed by the Israeli army. During the three-mile march, protesters chanted "Free, free Palestine!" Speakers pointed out the connections between the U.S. government's funding of Israel's terror.</P>
<P>On April 29, a small but spirited counterdemonstration confronted Chicago's "Walk for Israel," an annual fundraiser for Israel. Amid sickening speeches by politicians vowing to help Israel "defeat terrorism," protesters raised their voices to declare, "Palestine will be free!" </P>

