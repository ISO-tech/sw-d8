<p><a name="Water"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend Cochabamba water rights</b></font></p>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Josh On</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Oscar Olivera, executive secretary of the Cochabamba Federation of Factory Workers in Bolivia, marched outside Bechtel's international headquarters here last week. Local supporters joined him to protest Bechtel's ongoing push for water privatization around the world.  </P>
<P>Olivera helped to lead a successful struggle in Cochabamba last year against Bechtel, which had laid claim to all the water in the area, including rainwater collected by individual peasants. </P>
<P>Bechtel hiked water prices by 300 percent, forcing many poor residents to choose between water and food. Today, the residents of Cochabamba control their own water resources and distribution. But Bechtel will not rest and is now suing the Bolivian government for $25 million for loss of potential profits.  </P>
<P>Tuesday's protest was focused on this latest assault. People carried signs reading "Who owns the clouds?" and "No corporate water grab."</P>

