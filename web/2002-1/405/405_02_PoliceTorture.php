<font face="Times New Roman, Times, serif" size="5"><b>Judge orders probe of police torturers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 3, 2002 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO ACTIVISTS won an important victory last week when a Cook County judge appointed a special prosecutor to investigate charges of torture by Chicago police.</P>
<P>In 1993, Chicago Police Commander Jon Burge was fired for overseeing the use of torture against African American suspects at Area 2 and Area 3 police headquarters. But for years, Cook County State's Attorney Dick Devine has refused to investigate these allegations--either to seek indictments against Burge's gang of torturers or to reexamine convictions won on the basis of false confessions.</P>
<P>Last week, Judge Paul Biebel did what Devine should have by appointing a special prosecutor. Biebel ruled that Devine has a conflict of interest--having been a top prosecutor when Burge and his gang were on the rampage and, later, a defense attorney for a firm that defended Burge.</P>
<P>But if Devine has a conflict of interest, so does Chicago Mayor Richard Daley--who was state's attorney when most of the tortures took place. The fact that high-level officials like Devine and Daley have much to lose from a thorough investigation explains why activists and lawyers had to mount a campaign of pressure to get an investigation.</P>
<P>More than 60 people who were tortured by Burge as early as 1973 have come forward with horrifying stories of electric shock, Russian roulette and suffocation carried out in South Side police stations by Burge and other cops.</P>
<P>While Burge enjoys his retirement in Florida, his victims still languish in prisons. Among them are the Death Row 10--a group of men who ended up on death row after being tortured by Burge. Not a single member of the Death Row 10 has gotten a new trial. </P>
<P>"This is an incredible victory," said Stanley Howard, a member of the Death Row 10. "We've been fighting for a long time. We just have to make sure that we keep up the pressure on the special prosecutor to hold him accountable."</P>

