<font face="Times New Roman, Times, serif" size="4">NYC transit workers kick off contract campaign</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"People are ready to strike"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By David Thurston and Michael Ware</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 3, 2002 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK&#150;"Cutting our benefits is not an option," yelled out Roger Toussaint, president of Transport Workers Union (TWU) Local 100, to a sea of nearly 12,000 Metropolitan Transit Authority (MTA) workers.</P>
<P>Workers gathered in front of MTA headquarters in midtown Manhattan to protest a proposal that would make TWU members make up for budget shortfalls by paying premiums for family medical benefits. "Cut from the bosses, not the people who move 7 million New Yorkers a day," Toussaint told the crowd. "They want to cut Central Services," said one worker. "That means that if I go to the hospital, they say we no longer cover anesthesia. You have to pay."</P>
<P>Though the rally specifically targeted the MTA's attempts to cut health benefits, some workers think the MTA is after more. "They're threatening our members before contract negotiations," one TWU marshal told <I>Socialist Worker. </I>"Later on, they'll give us back health benefits in exchange for broadbanding rights, which means taking away seniority. They would have the right to tell you when you work and where you work. They want to get control of our members."</P>
<P>Local 100's contract with the MTA expires December 15, and many workers still have bitter feelings about the 1999 contract battle. Then-Mayor Rudolph Giuliani illegally threatened huge fines and even arrest just for saying the word "strike." </P>
<P>A transit strike would have shut New York down during the busy holiday season. The old-guard leadership knuckled under and an unnecessarily weak contract was the result. Reformers from the New Directions caucus gained control of the union soon afterwards. </P>
<P>"People are ready to strike if they have to," said a station worker, expressing the sentiments of many fed-up MTA employees. Public-sector strikes are illegal under New York's Taylor Laws.</P>
<P>TWU Local 100 mobilized a third of its membership, even during the weekday rush hour, as a show of its commitment to win a good contract in December. As one marshal said, "Without TWU out there 24 hours a day, 365 days a year, there is no city. We aren't going to take any foolishness from the MTA."</P>

