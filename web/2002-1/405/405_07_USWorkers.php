<font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Are U.S. workers part of the problem?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 3, 2002 | Page 7</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE UNITED States is the wealthiest nation in the world, with one of the highest per capita incomes among industrialized countries. With only 4 percent of the world's population, the U.S.'s per capita consumption is 14 times greater than the low-income countries with 40 percent of the world's population.</P>
<P>Taken at face value, these statistics seem to demonstrate that the vast majority of Americans share an insatiable desire for luxury goods, with a devil-may-care attitude toward the world's less fortunate, not to mention the environmental destruction of the planet. On this basis, many people who seek an end to global inequality view the vast majority of the U.S. population as part of the problem, not part of the solution.</P>
<P>This view is profoundly mistaken, for it ignores the fact that the U.S. itself is the most unequal society--with one of the highest poverty rates--in the industrialized world.</P>
<P>The typical low-wage worker in the U.S. earns 44 percent less than in Europe. And the percentage of U.S children who are poor is twice as high as in other advanced economies. Just 60 percent of U.S. workers receive health care coverage from their employers, while half have no pension coverage of any kind. </P>
<P>A youth between the ages of 18 and 24 from the richest 20 percent of U.S. families is more than 10 times as likely to obtain a college degree than a student from the poorest 20 percent. The private wealth of the top 1 percent of the U.S. population amounts to more than the bottom 92 percent of Americans.</P>
<P>Life is far from a continuous spending spree for most people in the U.S., who experienced more and more difficulty paying for basic necessities throughout the economic boom of the 1990s. According to the Economic Policy Institute, "For the typical household, rising debt, not a rising stock market, was the big story of the 1990s."</P>
<P>By 1999, the total value of all outstanding debt was greater than the disposable income of all U.S. households. And skyrocketing debt is no longer a problem that plagues only the poor. About 49 million Americans reported in 1995 that at least once in the last year they had trouble paying bills such as mortgage, rent, utilities or medical care, according to the U.S. Census. About 14 percent of middle-income families have debt payments equal to more than 40 percent of their income; 9 percent have at least one bill that is more than 60 days overdue. </P>
<P>The number of people declaring bankruptcy doubled in the 1990s--and not because they unwisely binged on luxury goods. Forty percent of all personal bankruptcies are due to medical bills people are unable to pay.</P>
<P>While families with incomes over $100,000 spend just 3 percent of their incomes on health care, those earning $45,000 spend 6 percent, and families making under $10,000 spend 17 percent.</P>
<P>The economic boom of the 1990s did benefit the upper middle class--the wealthiest 20 percent of Americans, who now earn nine times more than the bottom 20 percent. Everyone else, however, lost ground during the stock market boom.</P>
<P>The majority of U.S. households have no stock holdings of any kind, including 401Ks or IRAs. Only a third of households own stock worth $5,000 or more. Nearly 75 percent of all the gains from the stock market boom went to the top 10 percent of the population.</P>
<P>Inequality has grown more still since the onset of recession last year. Roughly 2.3 million workers lost their jobs last year, and 2 million lost their health coverage. The number of full-time workers forced into part-time jobs rose to 4.3 million last year. A recent AFL-CIO poll showed 55 percent of American workers between the ages of 18 and 34 see their economic future as nothing more than a series of low-paying jobs with no benefits.</P>
<P>The same forces that have created unprecedented inequality on a global scale have also created an unprecedented concentration of wealth at home. The working class in the U.S. has every interest in fighting for an alternative.</P>

