<font face="Times New Roman, Times, serif" size="5"><b>The myth behind privatization</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 3, 2002 | Page 4</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><B>Dear <I>Socialist Worker,</I></B></P>
<P>A few years ago, right-wing hacks thought that they had found a new way to attack public education. In 1997, Arizona, along with 20 others states, passed a program where taxpayers could earn tax breaks by donating money to help public school students attend private schools. </P>
<P>Right-wingers who backed such programs hailed them as aid for poor and minority students in failing schools. </P>
<P>But a recent study exposed the tax credit program for the scam that it is. The Arizona State University research found that: 81 percent of the money went to help parents pay tuition for students that already attended private schools; less than 1 percent of public school students were able to use the money to transfer to private schools; and the wealthiest one-quarter of public school districts received 52 percent of the money--and most of that money was used to send kids on field trips and pay for items such as band uniforms.</P>
<P><B>Jeff Bale, </B>Washington, D.C.</P>

