<font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>No recovery for workers</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 3, 2002 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">GEORGE W. BUSH hailed the latest economic statistics, which showed a growth rate of 5.8 percent in the first quarter of 2002, as "a good sign that we're on the path to long-term recovery." But the business press and CEOs had a different message: Even if the recession is really over, hard times for workers are not.</P>
<P>Although the economy grew by its fastest rate in two years, most of this was due to the restocking of inventories diminished last year when Corporate America emptied shelves with fire-sale price cuts. "This will give the economy a boost only temporarily," the <I>Wall Street Journal </I>admitted.</P>
<P>Bush's increased military spending also contributed to growth, say analysts, but not enough to carry the economy for the long term--leaving aside the question of its horrific social costs.</P>
<P>Because of low interest rates, consumer spending was strong in early 2002. But this can't go on forever with household debt still at record levels. Meanwhile, business spending on capital investments--factories, machinery, offices and the like--remains low as companies stagger under their own record debt burden.</P>
<P>This overhang of corporate debt led to the recent jitters in the stock market as big-name corporations continued to report stagnant profits. AOL Time Warner, for example, was forced to write off $54 billion of its value as worthless--the largest loss in corporate history. </P>
<P>Meanwhile, the U.S. dollar was also losing value on currency markets--a sign that foreign investors are getting nervous. And, it turns out, corporate books in recent years have been cooked, Enron-style--by what <I>Business Week </I>called "weak leadership, corrupt analysts, complacent boards and questionable accounting."</P>
<P>Whether or not a recovery has begun, CEOs are out to increase their still-low profits--at workers' expense. That means sticking it to us, with everything from increased health insurance costs to wage freezes--and the constant threat of a pink slip.</P>
<P>A system that shuts workers out in good times and makes them pay in bad ones--that's the reality of their free market. We need a socialist alternative based on workers' control of society to meet human needs, not corporate greed.</P>

