<font face="Arial, Helvetica, sans-serif" size="2"><b><i>SW</i> SPECIAL FEATURE</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Voices from the West Bank</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 3, 2002 | Pages 6 and 7</font></p>

<P><font face="Times New Roman, Times, serif" size="3">SECRETARY OF State Colin Powell may have called on the Israeli government to cooperate with a United Nations (UN) fact-finding mission into the devastation of the Jenin refugee camp in the West Bank. But don't think he's gone soft.</P>
<P>"Clearly, people died in Jenin," Powell told a Congressional hearing last week. "People who were terrorists died in Jenin. And in the prosecution of that battle, innocent lives may well have been lost." So the Palestinian dead in Jenin were either "terrorists" who deserved to die--or at worst, unintended "collateral damage."</P>
<P>Powell's see-no-evil attitude toward Israeli atrocities is par for the course in official Washington. In fact, the senators who questioned him at the hearing last week wanted to know why the administration wasn't celebrating Israel's operation in Jenin. And an Associated Press story about the hearing reported: "There has been no comparable outcry in response to suicide bombings by Palestinian militants that have killed hundreds of Israeli civilians."</P>
<P>Incredible! Washington politicians and the U.S. media have done nothing but cry out about Palestinian suicide bombings--as justification for Israel's use of massively greater force against the whole of the Palestinian population.</P>
<P>If ordinary people in the U.S. knew what was done by their government's main ally in the oil-rich Middle East--and carried out with weapons and equipment supplied by the U.S.--it would horrify them. The truth about Israel's reign of terror against Palestinians needs to be told. Here, <I>Socialist Worker</I> hears from some voices of resistance.</P>
