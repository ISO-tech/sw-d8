<font face="Times New Roman, Times, serif" size="4">Don't let right-wingers intimidate us into silence</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>We can stop these bigots</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 3, 2002 | Page 4</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><B>Dear <I>Socialist Worker,</I></B></P>
<P>Since the beginning of the year here at the University of Vermont, a right-winger on campus has made it his crusade to try to "purge" the International Socialist Organization (ISO)--which he calls a "parasitic cult of pseudo-intellectuals"--from campus.</P>
<P>He has personally attacked members in the school newspaper, making a point of stigmatizing one member as a "foreign national" at a time when anti-immigrant feeling is high. He even went so far as to distribute a poster that called a student member "the best case for abortion I've ever met."</P>
<P>On February 27, this right-winger tried to come to a private ISO meeting. Because none of us felt that we could speak freely, we democratically decided to ask him to leave. He refused, and we canceled the meeting.</P>
<P>The next day, we spoke with the Student Government Association (SGA) president and were assured that we had broken no rules. Two weeks later, however, we were informed that the SGA had voted to suspend the ISO and take away its budget until next January. </P>
<P>We were furious and appealed immediately. Because we organized, 75 community members came to a hearing and stood in solidarity with the ISO, with many speaking in our defense. We showed how this right-winger had created an atmosphere of hate and violence toward anyone with dissenting ideas.</P>
<P>In the end, 14 student senators changed their votes in our favor, and the ISO was cleared. More importantly, we humiliated the right-wing bigots who thought that their "McCarthyite" tactics had finally won out. This was a victory for all minority and dissenting groups on campus--and couldn't have been won without the huge community support we received. </P>
<P><B>Chris Pedler, </B>Burlington, Vt.</P>

