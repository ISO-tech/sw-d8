<font face="Times New Roman, Times, serif" size="5"><b>Fighting for the millionaires</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 3, 2002 | Page 4</font></p>

<P><font face="Times New Roman, Times, serif" size="3">MULTIMILLIONAIRE Republican candidate for California governor Bill Simon struck a blow for fellow rich people everywhere in late April. When incumbent Gov. Gray Davis called for Simon to release his tax returns to prove that he had paid his "fair share," Simon refused.</P>
<P>"I mean, that sounds like somebody coming out of Moscow, you know?" Simon fumed on KSFO radio. "'Their fair share.' You know, Karl Marx talked like that...[like] there's going to be Big Brother that's going to decide what your 'fair share' is. Quite frankly, I don't need Big Brother to tell me if I'm behaving myself."</P>
<P>But when pressed for information, Simon was unable to tell reporters whether he had paid California income taxes at all last year. After all, he explained, his returns are "fairly complex."</P>
<P>Simon's rabid outburst didn't appear to upset his Republican handlers. They argue that the super-rich candidate refusing to disclose his financial information just makes him more appealing to voters. "There's a 'Mr. Smith' quality to Simon," said GOP strategist Ken Khachigian. "He's not scripted. It's refreshing that somebody speaks their mind from time to time."</P>
<P>--<I>San Francisco Chronicle, </I>April 18, 2002</p>

<p><font face="Times New Roman, Times, serif" size="5">The new spring line--for racists</font></P>

<P><font face="Times New Roman, Times, serif" size="3">EXECUTIVES AT the clothing manufacturer Abercrombie &amp; Fitch just don't understand what all the fuss is about.</P>
<P>In April, several Asian American groups lodged complaints about a new line of T-shirts that the company was introducing. Selling for $25 each, the shirts featured caricatures of Asians (complete with cartoonish, slanted eyes) and had slogans like "Wong Brothers Laundry Service: Two Wongs Can Make it White."</P>
<P>"This is really blatant. It's just like the 1800s," said Rev. Norman Fong, program director at San Francisco's Chinatown Community Development Center.</P>
<P>When groups complained that the shirts are racist, the chain withdrew them. But puzzled executives didn't really understand what the problem was. As company spokesperson Hampton Carney explained, "We thought they were cheeky, irreverent and funny and everyone would love them."</P>
<P>--Reuters, April 19, 2002</P>

