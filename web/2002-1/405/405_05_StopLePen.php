<font face="Times New Roman, Times, serif" size="5"><b>How can Le Pen be stopped?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 3, 2002 | Page 5</font></p>

<P><font face="Times New Roman, Times, serif" size="3">ACROSS FRANCE and around the world, people are discussing how best to defeat the menace of Jean-Marie Le Pen.</P>
<P>Jacques Chirac wants voters to support him to protect France's "honor" and "democracy," and Socialist Party leaders, including Jospin, have echoed this call. But a vote for Chirac to defeat Le Pen is mistaken.</P>
<P>First, mainstream politicians like Chirac bear responsibility for Le Pen's rise--because their policies have fed despair and disillusionment. Chirac himself represents everything that people hate about the status quo. He's mired in corruption scandals having to do with campaign financing and kickback schemes dating to his days as mayor of Paris.</P>
<P>Voting for a sleazebag like Chirac--even as a "vote for democracy" against Le Pen--will only reinforce France's bankrupt political system and produce more disillusionment that will benefit the Nazis.</P>
<P>Chirac has already shown that he can't be trusted to keep Nazis from gaining influence. Chirac's party handed Le Pen's National Front its first major political victory in 1983 by inviting the Nazis to join a "united right" election list--a decision that Chirac publicly defended. And it's an open secret in France that Chirac has met in the past with Le Pen to discuss electoral deals.</P>
<P>The best way to fight Le Pen is to mobilize in the streets. This can provide a real alternative to the status quo by bringing together workers and students, immigrant and non-immigrant, to fight against racism and for better living standards for all.</P>
<P>But mobilizing in the streets is critical for another reason. Le Pen wants to turn his voter support into a mass Nazi movement that can terrorize society, attacking immigrants, unionists and antiracists. A fighting movement that confronts Le Pen and his Nazis can smash the fascists where they ultimately want to use their power--on the streets.</P>

