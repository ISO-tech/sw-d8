<font face="Times New Roman, Times, serif" size="4">Students for Justice in Palestine suspended at UC-Berkeley</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>An attack on our right to protest</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sid Patel</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 3, 2002 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ADMINISTRATORS AT the University of California-Berkeley have barred a Palestinian rights group from campus and threatened its members and supporters with suspensions --all for the "crime" of protesting. The university's campaign against Students for Justice in Palestine (SJP) is a blatant attack on free speech--at the very campus where the 1960s Free Speech Movement spurred on a decade of student activism.</P>
<P>The administration says that it took action because of an April 9 demonstration organized by the SJP that drew more than 1,200 protesters outraged by Israel's onslaught in the West Bank. As part of the demonstration, close to 100 people were arrested for taking part in a nonviolent sit-in in Wheeler Hall to call on the UC system to divest from Israel.</P>
<P>Last week, administrators announced that they were suspending the SJP from campus--on the grounds that the April 9 demonstration was an "academic disruption."  This means that the group can't organize events or meetings on campus, distribute literature or hold information tables. The 41 students arrested in the sit-in have also been told that they may be suspended for as long as a year.</P>
<P>This is clearly a political attack on supporters of Palestinian rights in particular--and on the right to protest in general. The April 9 protest didn't threaten anyone on campus--and the "disruption" was caused not by demonstrators, but by police who locked the building down. And Chancellor Robert Berdahl implemented his new "zero-tolerance" policy just two days before April 9. Clearly, he had the SJP in mind.</P>
<P>But activists aren't backing down without a fight. The SJP is organizing a free-speech campaign that has already brought in hundreds of letters of support from across the country. Plus, activists are going ahead with plans for a demonstration in early May.</P>
<P>"In the end, the university can't stop us from exercising our freedom of speech, because there are many more people on campus who feel the way we do," SJP member William Youmans told a reporter.</P>
<P>If the UC administration's attack on a pro-Palestinian political group succeeds, it will have a chilling effect throughout the country. We have to fight for our right to speak out.</P>
<P><I>Call Chancellor Berdahl at 510-642-7464 to demand that the ban on the SJP be lifted.</I> </P>

