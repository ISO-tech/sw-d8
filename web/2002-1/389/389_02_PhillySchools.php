<P><font face="Times New Roman, Times, serif" size="4">For-profit takeover of Philadelphia schools</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Selling schools to the highest bidder</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b><font face="Arial, Helvetica, sans-serif" size="2"> | January 11, 2002 | Page 2</font></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>PHILADELPHIA'S PUBLIC schools are being taken over by one of the most notorious for-profit education companies in the country. On December 22, the state government announced it was transferring control of the city's school district.</P>
<P>Threats of a takeover have been circulating since 1998, when former Gov. Tom Ridge--now George W. Bush's chief of homeland security--pushed through a law allowing the state to take control of ailing districts. This past summer, Ridge paid $2.7 million to Edison Schools--the notorious for-profit company that runs 136 schools in 22 states--for an "evaluation" of Philadelphia schools.</P>
<P>Surprise, surprise! Edison's report suggested that a for-profit company should be hired to run the district. Guess who got the deal?</P>
<P>Edison Schools will be paid $101 million over the next six years to run 45 of the district's lowest performing schools--and to provide "consulting services" to the rest of the district.</P>
<P>Edison, which recently spent $430,000 on an ad campaign to "sell" the takeover to skeptical city residents, claims that it is "reinventing public education." But "ripping off" is a far better description of what the company does.</P>
<P>The company is notorious for cutting educational corners to save cash. Edison routinely increases class sizes, hires inexperienced teachers, cuts special education classes and services for disabled students, and dumps "problem students"--often minorities. At some Edison schools, the yearly turnover rate for teachers is above 50 percent.</P>
<P>If schools got report cards, nearly all of Edison's would flunk. Education experts say the company plays a numbers game by emphasizing monthly testing of students' progress--and then inflates the average by "omitting" the scores of students who perform poorly.</P>
<P>Edison has such a poor track record that parents in New York City voted by a 5-to-1 margin against a recent attempt by Edison to get its hands on several city schools. But none of that mattered to current Gov. Mark Schweiker or Philadelphia Mayor John Street.</P>
<P>Street initially vowed to fight the takeover. But when the state government agreed to provide most of the money that goes to Edison, he accepted the deal.</P>
<P>It doesn't seem to matter to Street that the state is proposing to pay for the Edison deal by cutting more than 500 teaching positions, reducing employee benefits and privatizing bus routes and other services.</P>
<P>And Philadelphia residents won't even have a chance to vote on the deal--though their children will bear the consequences of Edison's latest "business venture." "They have nothing to lose if they fail," parent Steve Ashbridge told the <I>Philadelphia Daily News</I>. "But our kids could lose everything. It isn't right."</P>
<P>Angry parents, students and more than 30 community groups have formed Philadelphians United to Support Public Schools. In particular, student and youth organizations have led the way by organizing a protest of more than 1,000 at the state capitol.</P>
<P>The Philadelphia Federation of Teachers (PFT) has challenged Edison's takeover in court. But the union has been slow to rally members to action. PFT President Ted Kirsch is calling the takeover a "fait accompli"--despite teachers' outrage at the deal.</P>
<P>During the union's last round of contract negotiations 18 months ago, teachers voted overwhelmingly to strike--in part because of anger at the rumors of a state takeover. But the union postponed a walkout for several months--and then agreed on a contract that contained no safeguards against a takeover.</P>
<P>Now Edison will get another chance to "respond like Henry Ford in the 1920s and try to run schools as if they were automobile manufacturing plants," as one educator put it.</P>
<P>This is another face of the attack on our public schools. We can't let the politicians get away with it--whether they're in Philadelphia or Washington, D.C.</P>

