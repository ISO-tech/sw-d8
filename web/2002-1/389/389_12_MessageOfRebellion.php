<P><font face="Times New Roman, Times, serif" size="2"><B>REBELLION IN ARGENTINA</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Argentinians send message of resistance</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Rebelling against free-market misery</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 11, 2002 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>LEE SUSTAR reports on the struggle in Argentina.</P></B>
<P>THE UPRISING in Argentina that overthrew two governments in a week last month has sent a powerful message to bankers and politicians the world over. Mass struggle can stop the free-market policies pushed by the International Monetary Fund (IMF).</P>
<P>And that's a lesson that can inspire millions around the world. "The antiglobalization movement and traditional left-wingers are likely to hold up Argentina as an example of how market-oriented reforms can self-destruct," worried David Hale, chief global economist at Zurich Financial Services.</P>
<P>With the suspension of Argentina's $132 billion foreign debt after the fall of President Fernando de la R&uacute;a last month, panic began to spread in the corporate boardrooms and government offices in Washington and Europe.</P>
<P>The French government sent an emergency message to the new president, Eduardo Duhalde, pressuring him to do "everything in your power to look after our companies, which have invested much in Argentina."</P>
<P>The Bush administration is trying a different tack. During an appearance on NBC's <I>Meet the Press</I> last weekend, U.S. Treasury Secretary Paul O'Neill said, "You have to have no heart not to care about what's going on in Argentina&#133;I think we should care about it very much."</P>
<P>But it was O'Neill's "caring" Treasury Department and the IMF's bureaucrats who insisted that Argentina keep its peso linked to the dollar to ensure repayment of debts--no matter how much Argentinians suffered.</P>
<P>It was these "caring" people who demanded one round of austerity measures after another--and even withheld emergency loans to Argentina when the de la R&uacute;a government wavered on its plans to freeze bank accounts.</P>
<P>Spurred on by this threat, the government did freeze bank accounts--and tried to confiscate pensions, cut public-sector workers' wages and slice another 20 percent from the government budget. But ordinary people in Argentina said no.</P>
<P>The uprising last month began with food riots in Buenos Aires. Within a week, masses of people took to the streets, defying a martial law crackdown that left 27 dead.</P>
<P>De la R&uacute;a and his economics minister Domingo Cavallo had no choice but to flee--just as interim President Adolfo Rodriguez Saa had to resign following protests a week later.</P>
<P>The "Argentinazo" was the culmination of years of struggle. The most spectacular battles were fought by a new movement of the unemployed, the piqueteros. They regularly blockaded roads and organized direct action to force local and regional officials to distribute food aid or implement emergency jobs programs. Spurred on by the piqueteros, union leaders called seven general strikes in the last two years.</P>
<P>Last week, the Peronist party, which controls the majority in Argentina's National Assembly, elected Duhalde, who claims the authority to serve out de la R&uacute;a's term until 2003. Duhalde has tried to capture the popular anger against the IMF and the bankers. "It's time to tell the truth," he told the Argentine Congress last week. "Argentina is destroyed. This model has destroyed everything."</P>
<P>What he didn't say is that his Peronist party instituted the free-market "model" in the 1990s under the government of Carlos Menem. And Duhalde's plan for devaluation will have a severe impact on workers and the poor.</P>
<P>As Duhalde announced his plan on January 6, anger was growing. There is widespread sentiment for early elections to replace the government of Duhalde, which is full of officials known for their corruption.</P>
<P>The devaluation plan may burn some foreign investors. But Duhalde is determined to safeguard the interests of Argentine business and its hangers-on.</P>
<P>Activists are planning protests to demand new elections. Some are calling for a popular constituent assembly to form a new kind of government altogether.</P>
<P>And as <I>Socialist Worker</I> went to press, there were reports of new strikes to defend jobs, as well as demonstrations by <I>cacerolazos</I>--people carrying empty pots to symbolize their hunger, just as they did during the mass uprising against de la R&uacute;a.</P>
<P>De la R&uacute;a's ouster was a tremendous victory--not just for working people in Argentina, but everywhere that workers are forced to pay the price for the bosses' crisis.</P>

