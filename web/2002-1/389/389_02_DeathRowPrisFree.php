<P><font face="Times New Roman, Times, serif" size="5"><b>Death row prisoner freed after 18 years</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 11, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WHEN JUAN Melendez was released last week after nearly 18 years on Florida's death row, he became the 99th death row prisoner freed in the U.S. since 1973. "They can give me a billion dollars, and they can't pay for what they did to me," Melendez told reporters.</P>
<P>Before Melendez's original trial, another man confessed several times to law enforcement officials that he was guilty of the murder Melendez was charged with. But these confessions were kept secret from Melendez's original lawyer. In fact, the evidence that exonerated Melendez was only discovered by chance two years ago--when a lawyer was cleaning out files in his office and came across a transcript of the confessions.</P>
<P>"I'm happy to finally have it over and to have Juan released," said Marty McClain, an attorney who represented Melendez during his appeals. "But it really is a sad day that the system allowed this to happen and for it to go on so long."</P>

