<P><font face="Times New Roman, Times, serif" size="4">Eight years of attacks on working people</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Good riddance to Ghouliani</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Michael Watson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 11, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>RUDOLPH GIULIANI gave working people plenty of reasons to hate him during his eight long years as mayor of New York City. But he left office at the end of last year a hero.</P>
<I><P>Time</I> magazine named him 2001's "Person of the Year"--for his performance in front of the cameras after the September 11 attack on the World Trade Center.</P>
<P>Giuliani may have come off as a human being during the tragedy. But his record as mayor tells a different story--that he helped to wreck the lives of thousands of people with eight years of racist, anti-labor, anti-poor policies and his unwavering support for New York's murderers in blue.</P>
<P>New York schools have become some of the most overcrowded and run-down in the country. But Giuliani scapegoated teachers, refusing to raise their salaries even minimally without "merit pay." In 1995, he slashed the budget for the City University of New York--and then had police attack students who dared to protest.</P>
<P>When transit workers prepared to go on strike in December 1999, Giuliani threatened each union member with jail for just using the word "strike"--and a daily $25,000 fine if they walked.</P>
<P>"Ghouliani"--as he was known to many before September 11--jumped on the welfare "reform" bandwagon, throwing 500,000 New Yorkers off the rolls. In order to collect benefits, most had to perform workfare jobs--previously unionized work, like sanitation--for less than $2 an hour.</P>
<P>Giuliani used "Quality of Life" laws to go after taxi drivers, street vendors, protesters, bicyclists--and basically anyone who showed signs of being poor. "Streets do not exist in civilized societies for the purpose of people sleeping there," <I>Time</I>'s Person of the Year once sneered. "Bedrooms are for sleeping."</P>
<P>But Giuliani's most enduring legacy may be his support for racial profiling and police murder. From the hideous torture of Abner Louima in 1997 and the murder of Amadou Diallo in a hail of 41 bullets in 1999, Giuliani relentlessly supported the NYPD Black and Blue.</P>
<P>At the same time, Giuliani showered massive tax breaks on Wall Street and the corporations.</P>
<P>Corporate media outlets like <I>Time</I> may pay tribute to this monster. But Giuliani's real legacy is eight years of racist scapegoating, police terror and attacks on education and unions.</P>

