<P><font face="Times New Roman, Times, serif" size="5"><b>A hip-hop voice of resistance</b></font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>MOVIES: <I>Jails, Hospitals and Hip-Hop,</B></I> written by and starring Danny Hoch, distributed by Kicked Down Productions.</P></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Brendan Sexton III</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 11, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WRITER, ACTOR and activist Danny Hoch is best-known for his one-man stage play "Some People," which won an Obie award in 1995 and was later distributed widely on HBO.</P>
<P>Hoch recently turned his latest play, "Jails, Hospitals and Hip-Hop," into a film. This is no easy task, especially for a play consisting of one actor who metamorphoses into many characters.</P>
<P>Hoch took his characters "on location" where their scenes are set--from a Rikers Island prison barge; to Havana, Cuba; to a hospital in the South Bronx. The film, masterfully edited by Brian A. Kates, intercuts these scenes with Hoch's live stage performances, including one for inmates in Elizabeth, New Jersey's Union County jail.</P>
<P>The film starts with a poem, "Message to the Bluntman"--a message from Corporate America to the silly thug wannabes in the hip-hop industry. Corporate America lets them know that their thuggery can't compete with the thuggery of American capitalism.</P>
<P>The meticulous attention Hoch pays to his characters can either make you cry, laugh hysterically or both. The film gives great insight into how Corporate America affects the lives of ordinary people every day--whether it's through the health care system, the prisons or police brutality.</P>
<P>Hoch is very aware of the profit motives that drive much of the prison industry boom and is sharp in his analysis of the effect of the corporatization of hip-hop and culture.</P>
<P>When voices of resistance are threatened by Attorney General John Ashcroft and Co., <I>Jails, Hospitals and Hip-Hop</I> is an inspiration to all fighting for a better world.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>SW talks with Danny Hoch</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>WHAT IS the film's poem, "Message to the Bluntman," about?</P>
</B><P>Corporate America's infringement on hip-hop culture, its infringement on people's freedom through the prison-industrial complex and hip-hop's confusion as to what its purpose is and what its power is.</P>
<P>Hip-hop is the last popular culture of resistance in the last millennium and the first culture of resistance at the beginning of this millennium.</P>
<P>In the '60s and '70s, Black, Latino and Native American radical politics was gaining momentum, and the government took great pains to squash it. Hip-hop culture comes out of that resistance.</P>
<B>
<P>WHAT DO you think about the "war on terrorism"?</P>
</B><P>What George Bush and his father's corporation--where Colin Powell and Dick Cheney are CEOs and George W. is the Chief of Operations--are doing is securing their prolonged profit.</P>
<P>It's an extension of what the U.S. has done to poor countries for years--in Panama, Iraq, Sudan, or any number of African or Latin American countries. But it's packaged to us as a war.</P>
<P>We kill more people. We're the most efficient terrorists. And the media thwarts any type of resistance by making it seem criminal.</P>
<P>The question is how do we make people realize that Bush is a criminal, a war criminal.</P>

