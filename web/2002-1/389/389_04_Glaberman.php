<P><font face="Times New Roman, Times, serif" size="5"><b>Devoted to the struggles of the working class</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 11, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Martin Glaberman, a Marxist, teacher and autoworker, died December 17 in Detroit at the age of 83.</P>
<P>Marty was a tireless advocate for workers and taught labor history at Wayne State University until he retired in 1989. He wrote books, pamphlets, essays and poems about workers and their struggles. He also helped to publish and distribute the writings of his comrade C.L.R. James, a West Indian Marxist.</P>
<P>Before he was a teacher, Marty worked on the Buick assembly line in Flint, Mich. As his son Peter told the <I>Detroit Free Press,</I> "He believed all his life that the working class had revolutionary potential."</P>
<P>Glaberman was born in Brooklyn, N.Y., and attended City College, where his political life began with the Young People's Socialist League. He attended Columbia University briefly, before dropping out to become a full-time organizer for the Workers Party.</P>
<P>Glaberman's book, <I>Wartime Strikes,</I> published in 1980, illustrates his basic perspective on rank-and-file militancy. It's informed by his experiences in helping to combat the "no-strike" pledge adopted by most unions during the Second World War.</P>
<P>In Detroit radical circles, Marty was well known for his deep knowledge of Marx, Lenin and James. In the 1960s, he led a study group on Marx's <I>Capital</I> for the leading contributors to <I>Inner City Voice</I>--a revolutionary Black community newspaper. Many of those who participated went on to found the League of Revolutionary Black Workers, which played a prominent role in the auto factories in Detroit from 1967 to 1974.</P>
<P>Marty's good humor, devotion to revolutionary working-class struggle and straightforward insights will be greatly missed.</P>
<B><P>Bill Roberts,</B> Chicago</P>

