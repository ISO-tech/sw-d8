<P><font face="Times New Roman, Times, serif" size="4">Protest UAW headquarters to defend Accuride workers January 14</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"If you're for solidarity, stand up"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 11, 2002 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>UNITED AUTO Workers (UAW) activists are planning to protest next week against the International's move to cut off strike benefits to locked-out Accuride workers in Henderson, Ky. About 425 workers at the wheel-making plant went on strike nearly four years ago and were locked out soon afterward.</P>
<P>Ron Gettelfinger, then the UAW regional director in the area, had urged strike action against Accuride's notoriously anti-union management. Today Gettelfinger is the handpicked candidate of top union leaders to become the UAW's next president.</P>
<P>And now he and the rest of the International Executive Board want to cut off strike benefits for Accuride workers--for the second time. Strike benefits were cut off the first time in May 2000, and the local was put under administration.</P>
<P>Now the board is even considering revoking Local 2036's charter. These threats are designed to pressure workers into accepting Accuride's contract proposal in a vote scheduled January 12.</P>
<P>But workers have repeatedly rejected this union-busting deal, which would allow management to change pensions and medical coverage at will and implement unlimited outsourcing. Only 80 to 110 jobs would remain--and there is no provision that all union members would be recalled.</P>
<P>The UAW Solidarity Coalition has called for pickets January 14 at UAW headquarters in Detroit and at Gettelfinger's home local in Louisville, Ky., to demand continued support for the Accuride workers. (Go to connect.to/uawsc on the Web for details.)</P>
<P>Here, BILLY ROBINSON, former president of Local 2036, writes about the stakes for labor in this struggle.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>UAW LOCAL 2036 was certified in 1979 by a ruling of the U.S. Supreme Court. It won its first contract with the former owner of Accuride, Firestone, after a four-and-a-half month strike across Christmas that year.</P>
<P>Twenty-one years to the day after signing that first contract, Local 2036 members were again forced to strike in order to save their union rights. Only this time, Accuride was owned by Phelps-Dodge, a copper conglomerate and the most anti-union company in the country.</P>
<P>Ron Gettelfinger, then Region 3 director, told me, "Take the members on strike." A month into a strike with no progress, members voted against the company proposal and voted to return to work unconditionally.</P>
<P>Every step of the way the International directed the strike/lockout. The next Region 3 director, Terry Thurman, told nearly 500 executive officers of locals from all over Region 3: "We'll be on the picket line one day longer" in support of Local 2036.</P>
<P>One month later, in the fall of 1999, Thurman cut off all benefits for Local 2036. He told me on the phone, "It's time for you to get on with your lives. We can't win that situation."</P>
<P>The membership thought differently. They chose to continue the picket and fight back.</P>
<P>We immediately went member to member to gather support, something the International should have done in March 1998. This made UAW President Steve Yokich mad.</P>
<P>In April 2000, the International Executive Board (IEB) summoned the Local 2036 officers to Detroit to a "show cause" hearing to explain why the local had not "presented a company proposal to the membership for ratification."</P>
<P>As I said before, the International directed every phase of negotiations and in November of 1998 directed the membership to "not vote" on another proposal until the "company changed it"--which is exactly what the members did.</P>
<P>In August 1999, the International said that since we had not voted on a contract, "it is a lost cause." Go figure.</P>
<P>At our "show cause" hearing, Yokich said, "You're not the first local that we have pulled the benefits from, far from the first, and you won't be the last." So I ask you now: Are you next?</P>
<P>In October 2000, after a hard battle fought by UAW members from all over the country, the IEB suddenly decided to restore benefits. They even doubled them for a period. Then in October 2001, they reduced them.</P>
<P>In November 2001, Accuride's latest proposal was voted down by 97 percent--with two-thirds of the membership voting. A month later, UAW officials told the officers of Local 2036, "Get on with your lives, it's a lost cause, and on January 15, 2002, we are going to stop all benefits, strike pay and health care. And if you don't ratify that contract, and without a doubt it is the worst one we have ever seen, then the IEB is going to pull your charter."</P>
<P>So the question is, why? Why didn't Local 2036 receive the support of the UAW--and why has the International gone so far as to threaten members who defy them and try to help? Shouldn't you be aware that you might be handling scab-produced parts?</P>
<P>If you believe in solidarity and don't want to be next, then it's time you started to do something about it. Join other activists in the struggle to take our union back--and throw out the company sympathizers who have taken over the UAW.</P>
