<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Israel's new excuse for a crackdown</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 11, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ISRAEL LAST week seized a ship that it claimed was smuggling weapons to the Palestinian Authority (PA). Israeli officials immediately went on the offensive, eager to proclaim that their brutal war on Palestinians had been justified all along. "The Palestinian Authority is infested with terror from head to toe," Israeli Chief of Staff Shaul Mofaz told reporters.</P>
<P>For anyone who supports the Palestinian struggle for liberation, the seizure of the ship--with its cargo of 50 tons of rockets, assault rifles and explosives--looks like a setup. It was certainly convenient coming on the eve of a visit by U.S. special envoy Anthony Zinni, who is pushing a plan that would include freezing illegal Israeli settlements--something that Prime Minister Ariel Sharon could never tolerate.</P>
<P>In fact, even as Israeli officials insisted that the ship was owned by a top PA official, British news agencies reported that it belonged to an Iraqi. And a U.S. official suggested that the ship could have been delivering arms to the Lebanon-based Hezbollah militia.</P>
<P>In any case, even a whole ocean liner packed with weapons wouldn't put the PA on equal footing with Israel's military.</P>
<P>But true to form, the U.S. mainstream media swallowed every word fed to them by the Israeli government. Sharon proclaimed that Arafat and the PA were preparing a "new wave of terror"--despite the relative calm of the past few weeks.</P>
<P>The Israeli military--which has killed more than 800 Palestinians during the last 15 months--is the real source of terror and violence. It has used the December suicide bombings in Israel as an excuse to step up its campaign of assassinations of Palestinian leaders, attacks on unarmed Palestinian demonstrators and outright invasions of PA territory.</P>
<P>"It was embarrassing and irksome to hear the chief of staff scolding the Palestinians for 'wasting their money on acquiring arms instead of seeing to their poor and hungry populace'--the words of a man whose soldiers&#133;harass Palestinians morning, noon and night, impoverish them and starve them," wrote David Grossman in Israel's <I>Ha'aretz</I> newspaper.</P>
<P>With or without the "discovery" of the ship, Sharon and the Israeli government are determined to continue their war on Palestinians.</P>
<P>We have to support the struggle of Palestinians--and demand that the U.S. government end all aid for Israel's reign of terror.</P>

