<P><font face="Times New Roman, Times, serif" size="4"><b>Cook and DuPage County transportation workers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Bridget Broderick</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--In a cold rain, about 50 members of the Amalgamated Transit Union (ATU) Local 1702 and their supporters picketed and rallied in front of the Cook-DuPage Transportation (CDT) offices December 14.</P>
<P>Four hundred CDT drivers, mechanics, dispatchers and office workers provide paratransit services for thousands of Chicago residents with disabilities who can't ride the Chicago Transit Authority.</P>
<P>After months of negotiations, union members were fed up with the unwillingness of CDT's owner to negotiate, and voted to authorize a strike in December.</P>
<P>Local 1702 President Erwin Dabney noted CDT had tried to divide workers by offering advances to drivers who didn't join the union and threatening to permanently replace strikers before a strike vote was even called.</P>
<P>The union is calling for a $1.11 raise over 3 years, more union security and respect on the job. Workers are angry that CDT was offering a $.30 raise in the first year, when their uniforms cost $100 a year.</P>
<P>Drivers have to work eight hours before they get a 20-minute break, and health care costs are becoming prohibitive--$40 per pay period for single members and $200 for some members with families.</P>
<P>Workers expressed outrage at the filthy conditions at the garage where they must contend with rats and unsanitary bathrooms. "Our employer gives us no respect," Minnie Williams, a paratransit operator for 12 years, told <I>Socialist Worker</I>. "I haven't had an accident in eight years, and yet we're screamed at. We need better wages and better conditions."</P>
<P>Members of Bricklayers Local 21, Service Employees International Union Local 1 and train and bus drivers' unions from around the city came to the ATU picket to show their support for the paratransit workers.</P>
<P>Dabney said that some of CDT's clients had also planned on attending the rally, but due to the poor weather, many couldn't get there.</P>

