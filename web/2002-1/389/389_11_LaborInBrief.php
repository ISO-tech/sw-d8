<p><a name="Cincinnati"></a><font face="Times New Roman, Times, serif" size="4"><b>University of Cincinnati professors</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Kerl</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CINCINNATI--Professors at the University of Cincinnati (UC) have been working without a contract since August 31. After a long stalemate, administrators finally agreed to resume negotiations with the UC chapter of the American Association of University Professors.</P>
<P>The university wants faculty to accept a contract that includes cutbacks in health care benefits totaling $2 million. Salaries are also a big issue, with professors demanding a 20 percent increase over three years.</P>
<P>Other issues include control over what goes into personnel files, governance of the university, domestic partner benefits and bias in the pay of female faculty.</P>
<P>The main sticking point is that the UC administration says it can't afford to give the professors more than an 8 percent raise. But the main campus has seen more than $1 billion in new construction during the past 10 years. And the university is planning construction of a brand new mansion near the school.</P>
<P>UC can afford a raise--but they won't give it without a fight.</P>

