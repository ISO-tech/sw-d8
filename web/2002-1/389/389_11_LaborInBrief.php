<p><a name="Harvard"></a><font face="Times New Roman, Times, serif" size="4"><b>Harvard University janitors</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mitch Lewis and Larry Lewis</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CAMBRIDGE--Hundreds of students joined more than 100 mostly immigrant workers at a rally called by Service Employees International Union Local 254. The rally called for a living wage for janitors at Harvard University.</P>
<P>"In seven years, I haven't got a raise," Harvard janitor Blanca Hurtado told <I>Socialist Worker</I>. "I have three kids, and it's not enough to support us all."</P>
<P>Addressing the crowd in both English and Spanish, State Rep. Jarrett Barrios of Cambridge stressed that the living wage campaign is about "basics--rent, health care, child care and cost of living." He pointed out that janitors' wages have dropped 30 percent since 1992. He also cited the Boston-area universities' policy of employing part-time janitors in order to avoid benefits payments and force workers into longer hours.</P>
<P>The rally, though large and enthusiastic, was only a beginning. With wage negotiations coming in January, we need to keep mass pressure on Harvard in the coming months.</P>
<P>Justice for janitors!</P>

