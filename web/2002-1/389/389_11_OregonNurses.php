<P><font face="Times New Roman, Times, serif" size="4">Hospital bosses put profits before patients</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Oregon nurses fight for quality care</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul Dean and Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 11, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>PORTLAND, Ore.--The 1,500-member Oregon Nurses Association (ONA) strike at Oregon Health and Science University (OHSU) continues into its fourth week without progress at the negotiating table.</P>
<P>Even though the ONA showed its willingness to make concessions during 12 hours of talks last Friday, hospital officials haven't budged. The ONA was going to put management's most recent unsatisfactory offer to a vote, but then management refused to sign a return-to-work agreement.</P>
<P>Hospital officials insisted that the voting time should be restricted and that the union's bargaining team should not be allowed to talk about the offer during the vote! But the ONA refused to be bullied. "We cannot allow management to restrict our voting process," read a statement on ONA's Web site. "This may be one of the most important decisions we will make in our careers."</P>
<P>"They're planning on punishing nurses by keeping them out longer," said Kathleen Sheridan, an ONA labor representative. "They want to keep them out in the rain and hope that the rain wears them down. But the nurses are just getting angrier," Sheridan told <I>Socialist Worker</I>.</P>
<P>Nurses on the picket line told <I>Socialist Worker</I> that a key issue is management's lack of respect. One nurse pointed out that the average age of nurses is 47 and nearing retirement.</P>
<P>The nursing shortage, at crisis levels across the country, will only get worse as those seeking degrees and good-paying jobs turn away from nursing with its long hours and bad working conditions.</P>
<P>"Management, instead of trying to solve the nursing shortage by making it a more attractive profession, has just said to the nurses, suck it up, deal with it," said Sheridan. "This is a national problem by every yardstick. We've told management they've got to deal with this. It's like a cup--you can only fill it up so far until it overflows. When a nurse's assignment gets too overwhelming, you can no longer provide quality care. That's why this is a critical issue for patients."</P>
<P>Across the U.S., nurses have gone on strike for similar reasons. Five hundred nurses at St. Catherine's of Siena Medical Center in Smithtown, N.Y., have been on strike since November 26 and without a contract since May 2001. In just the last year, nurses in Youngstown, Ohio; Palo Alto, Calif.; and Washington, D.C., have also gone on strike.</P>
<P>This trend affects other health care workers as well. Low wages and long hours led 13,000 home health care workers in Oregon to join the Service Employees International Union (SEIU) in December after years of fighting legislatively to get collective bargaining rights. And in 1999, 75,000 home health care workers in Los Angeles voted to join SEIU.</P>
<P>The strike at OHSU shows that health care workers will have to force administrators to put resources into paying nurses and delivering quality care instead of enriching their shareholders.</P>
<P>"I think this strike has changed these nurses' lives forever," Sheridan told <I>Socialist Worker</I>. "This has been a magnificent coming together of brothers and sisters who are so busy on the job they never have time to talk to one another. This has given them an opportunity--on the picket line and at other functions--for people to say I'm going to have a completely different relationship to my job. Management will know through our strike that we're not going to take this baloney anymore, and that we stand together. The whole concept of collective action remains only a concept in people's mind until they actually see the power that they have."</P>
<P>The ONA has also had great support from other unions. Teamsters have refused to cross picket lines, and the International Longshore Workers Union Local 8 has offered temporary jobs on Portland docks to striking nurses.</P>
<P>Nurses want to return to work, but not at any price. "I feel there is a strong message," said a nurse after finishing her shift at a phone bank to build support for the strike. "It will take a revolution to turn things around. We are in the engine of the revolution and should feel proud. OHSU management is trying to bully us and they should feel the power of our engine."</P>

