<P><font face="Times New Roman, Times, serif" size="5"><b>New York City Bloomberg protest</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By David Thurston</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 11, 2002 | Page 10</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--About 150 people gathered to protest on the second day of Mayor Michael Bloomberg's administration. "We're here to make sure that Mayor Giuliani's reign of terror does not continue in the new administration," Tina, from the Prison Moratorium Project, told the crowd.</P>
<P>Demonstrators demanded that Bloomberg put money toward the needs of New York's working people, like funding for schools, public housing, AIDS research and health care.</P>
<P>As Bloomberg came into office, he announced major budget cuts over the next few years as the city responds to the recession. Yet the recession has brought no end to corporate giveaways. New York officials "are giving hundreds of millions to corporations that don't need it," said a member of the Alliance for a Working Economy. "They're giving $1.6 billion to the New York Stock Exchange to build a new trading floor."</P>
<P>And just before leaving office, Giuliani also announced plans to fund two plush new stadiums for the city's baseball teams. Bloomberg, who has spent his life as a CEO, is prepared to fork over millions to allow the city to host the World Economic Forum at the end of January.</P>
<P>The protest was organized by a new coalition of groups, including the Black Radical Congress, the Prison Moratorium Project, Community Voices Heard, faculty and students from the City University of New York and other organizations.</P>
<P>If Bloomberg doesn't meet the coalition's demands by his 70th day in office, organizers have pledged to call for a major demonstration on his 100th day.</P>

