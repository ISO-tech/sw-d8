<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Behind the war threat in South Asia</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 11, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WITH 1 million Indian troops dug in along the border with Pakistan, India's right-wing government is following a script written in Washington, D.C.</P>
<P>After a December 13 suicide attack on the parliament building in New Delhi, the Indian government threatened an assault on "terrorists and the state that harbors them." The state is India's rival Pakistan--a dictatorship that responded with a buildup of its own.</P>
<P>When Pakistan's military ruler, Gen. Pervez Musharraf, offered a joint Indo-Pakistani investigation of the attack, India took another page from George W. Bush. Prime Minister Atal Behari Vajpayee refused to share evidence about the attack--and simply repeated its ultimatum that Pakistan hand over 20 suspected terrorists.</P>
<P>Now the two nuclear-armed powers in Asia are at the brink of war, with the epicenter in Kashmir, the disputed Muslim-majority state that is divided between Indian and Pakistani zones of control. The U.S. government's "war against terror" set the stage for the new confrontation.</P>
<P>Pakistan's regime wants to maintain its new cozier relationship with the U.S. since it signed up for Bush's war. But Musharraf can't afford to abandon the issue of Kashmir. India's main goal is to get the U.S. government to use its leverage to force Musharraf to withdraw his army's support for armed resistance groups opposed to India's control in Kashmir.</P>
<P>The U.S. can hardly refuse to put the squeeze on Musharraf--since India can claim that its warmongering is a logical extension of the "war on terrorism." Secretary of State Colin Powell--and even Bush himself--have repeatedly telephoned Musharraf to demand a stepped-up crackdown on Islamist militants, which Musharraf had begun even before the December 13 attack.</P>
<P>But as <I>Socialist Worker</I> went to press, Vajpayee was refusing to take "yes" for an answer from Musharraf, who has met every Indian demand except the extradition of the 20 suspects.</P>
<P>Keeping India on the brink of war is good politics for Vajpayee. Now that strategy--quietly supported by Washington--could lead to a war between two governments that not only possess nuclear weapons, but talk openly about using them.</P>
<P>The U.S. government is playing politics in one of the most explosive standoffs in the world.</P>

