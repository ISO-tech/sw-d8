<P><font face="Times New Roman, Times, serif" size="5"><b>Twain wasn't just about tall tales</b></font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>DOCUMENTARY: <I>Mark Twain,</B></I> directed by Ken Burns, a two-part, four-hour documentary, showing on PBS January 14 and 15.</P>
</font>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Donny Schraffenberger</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 11, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>DIRECTOR KEN Burns' new documentary shows Mark Twain as a writer unafraid to speak out against injustice.</P>
<P>Burns--best-known for his Civil War and jazz series for PBS--opens <I>Twain</I> with the writer's humble beginnings in the slave state of Missouri. One of Twain's childhood memories was of a dozen slaves chained together to be sold down river.</P>
<P>At the age of 17, Twain begins his journeys across this country. He's a riverboat pilot on the Mississippi, a prospector in Nevada and a journalist in San Francisco. He writes against the racist abuse of Chinese immigrants and exposes police corruption, a position that got him fired.</P>
<P>Twain is known for his <I>Adventures of Huckleberry Finn</I>--a best-seller of its day--which attacked racism and slavery. Later, Twain stood for women's suffrage, decried the hypocrisy of Christian missionaries and exposed the horrors of imperialism.</P>
<P>"I am an anti-imperialist," said Twain. "I am opposed to the eagle putting its talons on any other land." He denounced Belgian King Leopold's slaughter in the Congo, but no American publisher would print it.</P>
<P>If this film has flaws, it's stressing too much of Twain's family life and not enough of his political commentary. Still, Burns' documentary shows that Twain's writing is relevant today, and it will make you want to pick up one of his books in a hurry.</P>

