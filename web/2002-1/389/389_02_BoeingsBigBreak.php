<P><font face="Times New Roman, Times, serif" size="5"><b>Washington's help for Boeing</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 11, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>AIRCRAFT GIANT Boeing is having financial problems. But it may get by--with tens of billions of dollars in help from its pals in Washington.</P>
<P>Lawmakers snuck an incredible piece of corporate welfare into the Pentagon budget bill passed by Congress in December. The provision requires the Air Force to lease 100 Boeing 767s for use as tankers over a 10-year period.</P>
<P>Neither the Air Force nor the Bush administration asked for the planes. The provision was the brainchild of Sens. Ted Stevens (R-Ala.) and Patty Murray (D-Wash.).</P>
<P>Their last-minute amendment requires the Air Force to spend $20 billion on the leasing arrangement. That's more than it would cost to buy the planes.</P>
<P>There are laws prohibiting lease arrangements that cost more than direct purchases. But those laws were magically waived in this case.</P>
<P>With equipment overhauls to convert the commercial aircraft for military use, the federal government will spend more than $25 billion on planes that even the Pentagon doesn't want.</P>

