<P><font face="Times New Roman, Times, serif" size="5"><b>Portland, Ore., Bush protest</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul Dean</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 11, 2002 | Page 10</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>PORTLAND, Ore.--More than 500 people demonstrated against President Bush last weekend. The demonstration was organized with only a few days' notice when the visit was announced.</P>
<P>Because Oregon's unemployment is 7.4 percent, Bush chose to give a speech about unemployment nationally. Though he shed some crocodile tears in front of his invited audience, it's little comfort for those laid off by Freightliner, Boeing or the many high-tech companies around Portland. Like many across the country, Oregon workers have put up with cuts in pay and benefits to keep bosses' profits up.</P>
<P>Chanting "We didn't like your dad, and we don't like you," protesters had to face aggressive cops clad in riot gear and a military helicopter hovering above. The protesters also addressed the U.S. war on Afghanistan, health care and unemployment.</P>
<P>But Bush made clear that his idea of helping the unemployed doesn't go beyond tax cuts for the rich and bombs for the poor of other countries.</P>

