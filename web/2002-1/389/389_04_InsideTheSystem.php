<font face="Times New Roman, Times, serif" size="5"><b>No free speech at the library</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 11, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>A LIBRARY mural in Memphis, Tenn., is provoking heated debate among some people who haven't figured out that the Cold War with the ex-USSR is over.</P>
<P>Several local politicians are up in arms over the fact that the mural outside the new Memphis Central Library features the words, "Workers of the world, unite." The mural also features, among other things, depictions of the Cat in the Hat, a painting by Picasso and quotations by William Shakespeare and Salman Rushdie.</P>
<P>But officials only seem to be offended by the quote from <I>The Communist Manifesto</I>. "Over 100,000 Americans were killed in two wars trying to rid the world of Communism," fumed city councilman Brent Taylor. "We just don't feel a public place is appropriate to inscribe the motto of our enemy of 70 years."</P>
<P>Initially, the offended politicians suggested that the phrase be sandblasted off the mural. Commissioner Marilyn Loeffel, a self-described conservative Christian known for crusades against rock lyrics, suggested that offended citizens should try to block future money for the library and arts commission.</P>
<P>Or perhaps they should take matters into their own hands. "I've had a lot of people say, 'I've got a jackhammer,'" Loeffel quipped.</P>
<P>--<I>New York Times,</I> December 29, 2001</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>God hates Harry Potter</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>HARRY POTTER books are "an abomination to God." So says Pastor Jack Brock, head of Christ Community Church in Alamogordo, N.M.</P>
<P>"These books encourage our youth to learn more about witches, warlocks and sorcerers," Brock told reporters recently. "Harry Potter books are going to destroy the lives of many young people."</P>
<P>So Brock held a "holy bonfire" on December 30, where members of his congregation torched dozens of Harry Potter books to save the souls of vulnerable preteens. Members of the church sang "Amazing Grace" as they threw the books on the fire.</P>
<P>So God hates Harry Potter. But burning books is okay.</P>
<P>--Associated Press, December 30, 2001</P>

