<P><font face="Times New Roman, Times, serif" size="5"><b>Why won't they talk about Bush's slur?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 18, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE MAINSTREAM media will let George W. Bush get away with anything. During a press conference earlier this month, Bush used the racial slur "Pakis" in talking about escalating tensions between India and Pakistan.</P>
<P>"We are working hard to convince both the Indians and the Pakis there's a way to deal with their problems without going to war," Bush said. The insult is especially vile given that the phrase "Paki-bashing" is used, mainly in Britain, to describe attacks on any Asian or African immigrant.</P>
<P>But the establishment press doesn't seem to care. The <I>Washington Post</I> tucked its account of Bush's slur--and of the White House's unapologetic statement that the president "meant no disrespect"--as the next-to-last entry in its digest of minor Washington news items. And <I>the New York Times</I> tucked the story into the end of an article about White House policy in South Asia.</P>

