<P><font face="Times New Roman, Times, serif" size="4">Accuride workers picket UAW headquarters</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"Solidarity is forever"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 18, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ABOUT 100 members of the UAW picketed the union's Solidarity House headquarters in Detroit January 14 to protest the union's move to cut off benefits to locked-out Accuride workers of Local 2036 in Henderson, Ky.</P>
<P>"What you have here on this sidewalk is the real leadership of the UAW," said Miguel Chavarria, a member of UAW Local 22 and a member of the steering committee of the UAW Solidarity Coalition, which helped to organize the protest.</P>
<P>Don Anderson, a member of UAW Local 1700 at Daimler-Chrysler's Sterling Heights assembly plant, agreed. "The UAW has become a corporate union," Anderson told <I>Socialist Worker</I>.</P>
<P>Ali Canada, a committeeman at the Ford/Visteon parts plant in Ypsilanti, Mich., said that this struggle is an example of how he and other UAW members have to "fight on two fronts against management and the International leadership."</P>
<P>More than 400 workers at Accuride were locked out in March 1998 after going on strike to defend basic union rights. The UAW regional director at the time, Ron Gettelfinger, personally called for strike action. Now he's the International Executive Board's (IEB) leading candidate to become president of the UAW--and he'd like to sweep the Accuride struggle under the rug.</P>
<P>But former Local 2036 President Billy Robinson, locked-out workers and their supporters have built a campaign that has won the support of everyone fighting for progressive change in the UAW. "This is an issue for every UAW member," said Norm Crosby, who's a member of UAW Local 36 and a worker at Ford's plant in Wixom, Mich., which is slated for production cuts as part of that company's "restructuring" plan.</P>
<P>A similar picket in May 2000 forced the International to restore benefits after cutting them off for 14 months. This time, the IEB tried to pressure members of Local 2036 into finally voting for the contract, which would allow as few as 50 union members to be recalled--and with virtually no union rights.</P>
<P>If they failed to approve the deal in a vote that was scheduled for January 12, the benefits would be cut off and Local 2036's union charter revoked. But Accuride officials sent a mailing to every Local 2036 member with a list of those who would be recalled, forcing the International to cancel the vote.</P>
<P>Along with UAW Solidarity Coalition members, supporters of the UAW New Directions Movement turned out. UAW Concerned, an organization of retirees, mobilized turnout from across the Midwest. Others came from Maryland, West Virginia and California.</P>
<P>Jan Austin, a member of UAW Local 594 in Pontiac, Mich., and a former co-chair of the UAW Solidarity Coalition, embarrassed dozens of UAW officials and staff members into making donations for Accuride workers as they drove through the picket lines. Gene Austin, a delegate in Local 594 and also a member of the UAW Solidarity Coalition, said, "I am a proud UAW member, but I am determined to rid this union of corruption."</P>
<P>"Solidarity is forever, not just for a while or when it's convenient," said Gregg Shotwell, a member of Local 2151 in Cooperstown, Mich. "If the International manages to cut off Accuride workers, it will send a message across the UAW that if you stand up and fight, the International leadership will roll over."</P>
<P>After several hours picketing UAW headquarters, an administrative assistant to UAW Vice President Elizabeth Bunn agreed to communicate several questions from Robinson and Shotwell to UAW officials. The questions included: Why didn't the International support Local 2036? Did UAW officials understand that cutting off medical benefits will mean that some people will die? And, how does the UAW expect to organize the South when they stab workers in the back in Kentucky?</P>
<P>"I think that we sent them the message that there is division in their ranks," Robinson told <I>Socialist Worker</I>. "And it wasn't just in Detroit. By having people in Louisville and other places and by people wearing buttons, we've shown that this issue is not going away. It's time for this union to take a stand and fight."</P>
