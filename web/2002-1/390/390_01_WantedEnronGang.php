<P><font face="Times New Roman, Times, serif" size="4">WANTED:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The Enron gang</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 18, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE HEAT has been turned up on one of the crookedest gangs around. The thieves who presided over the meteoric rise and even more rapid collapse of energy giant Enron are facing a dozen government investigations and multiplying lawsuits.</P>
<P>Their accomplices are in trouble, too. The accountants who covered up Enron's crimes are on the hot seat--for ordering the destruction of audit documents a few days before the company publicly admitted its financial meltdown.</P>
<P>Then there are all those Washington politicians--Republican and Democrat alike--who gratefully accepted campaign cash from Enron in return for a few votes.</P>
<P>A year ago, Enron CEO Ken Lay was known to have veto power over energy policy in his old buddy George W. Bush's White House. Now Bush pretends that he can't remember the last time he heard from "Kenny Boy."</P>
<P>Enron was the toast of Corporate America during the 1990s--lavishly praised for its devotion to free-market principles and shrewd business strategy. Enron's collapse into bankruptcy in December exposed everything corrupt and cruel that lay beneath the rhetoric about a "miracle" economy.</P>
<P>Most striking was the way that the crooks at the top took care of themselves. As the financial problems mounted, top executives sold more than $1 billion worth of company stock in the years before the collapse.</P>
<P>But employees were forced to hold onto the Enron stock in their retirement savings accounts--even after the share price dropped to less than $1. "We had no idea that there was anything wrong until it was too late," said Charles Prestwood, a 33-year veteran of Enron and its predecessors, who saw his retirement savings vanish in a matter of weeks. "I feel like I've been betrayed."</P>
<P>The Enron disaster was overshadowed last year by Bush's war on terrorism. But every day seems to bring a new scandalous revelation about the company and its servants in Washington. The big question is whether a "smoking gun" proving criminal wrongdoing will be tied to the Bush gang in Washington.</P>
<P>Watching them squirm will be pure joy. But the larger lesson of Enron's collapse isn't about any one "smoking gun"--but the fact that the whole system is "smoking."</P>
<P>Enron didn't do business--as an energy trader or a buyer of political influence in Washington--much differently than any corporate giant. "Enron's woes aren't really a scandal at all," wrote columnist Andrew Leonard in <I>Salon</I> magazine. "[I]nstead, they're a magnifying glass allowing us to see clearly exactly how government and business operate today."</P>
<P>The point to remember is that the rulers of this system--whether they sit in corporate boardrooms or walk the corridors of power in Washington--put their own profits and power first. We need a socialist alternative to this corrupt system.</P>

