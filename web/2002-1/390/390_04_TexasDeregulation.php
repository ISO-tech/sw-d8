<P><font face="Times New Roman, Times, serif" size="4">Texas lawmakers back energy deregulation</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Another rip-off by the power bosses</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 18, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>Texans greeted the New Year with some bizarre and troubling news: The state is moving forward with plans to deregulate its energy industry.</P>
<P>Despite California's experience of blackouts, billions of dollars of public money down the drain and permanently jacked-up electricity rates, officials in Texas are insisting that deregulation will not produce similar problems here.</P>
<P>They point to the differences between the Texas and California deregulation plans, such as the fact that there is to be no cap on retail energy prices. In other words, if wholesale energy prices spike--as they did for California's utilities--price increases will be even more easily passed on to consumers.</P>
<P>Adding to my skepticism are the well-known connections between the Texas political establishment and its energy industry, in particular with the fallen giant Enron. In 1999, when deregulation was approved in Texas, Enron was the single largest patron of then-Gov. George W. Bush's political career. In fact, the chair of the Texas Public Utility Commission, which is supposed to oversee deregulation, is a former Enron executive--although he lied about that when he was appointed.</P>
<P>Thankfully, the city-owned utility where I live has opted out of deregulation for the time being. A statement on its Web site acknowledges that deregulation is unlikely to produce lower electric bills.</P>
<P>We must truly live in a corrupt system when politicians push forward with free-market policies that have been thoroughly discredited in California, Argentina and around the world.</P>
<B><P>Mike Corwin,</B> Austin, Texas</P>

