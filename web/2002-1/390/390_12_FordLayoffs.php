<P><font face="Times New Roman, Times, serif" size="4">Years of record profits repaid with big layoffs</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Ford makes workers pay</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Pranav Jani and Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 18, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NO SOONER had Ford Motor Co. revealed its plans to slash 35,000 jobs worldwide than CEO William Clay Ford declared that more cuts were to come. "We already have a war room going and a plan B in place," he said, as he announced cuts worth $4.1 billion to the company. </P>
<P>About 13,000 of the jobs to be eliminated are held by members of the United Auto Workers (UAW). In the late 1990s, Ford held down hiring by forcing mandatory overtime on UAW members. That helped the company to haul in profits of $9 billion in 1999--an industry record. Combined profits from 1997 to 2000 totaled $39 billion.</P>
<P>But now Ford is in the red--not only because of the recession, but disastrous decisions by management. And the company wants to make its workers pay the price.</P>
<P>Workers at Ford's Ranger truck plant in Edison, N.J.--slated to close under the layoff plan--are bitter. "It's terrible," UAW Local 980 member Johnny Greene told <I>Socialist Worker</I>. "I don't think I can even drive by here any more," said Greene, who works in the freight department. "I came to Ford when I was 18. I grew up at Ford. Layoffs are okay, because you might come back, but closure is final. I don't know what I'm going to do. Maybe work with the union. I'm too young to retire."</P>
<P>Under the 1999 UAW contract, Ford has to continue to pay laid-off workers 95 percent of their wages with supplemental unemployment benefits, often up to 18 months. But since most autoworkers work overtime to boost their pay, the reduction in income will be huge--and hard to match at another job.</P>
<P>"I've been here since '78," another Edison worker told <I>Socialist Worker</I>. "They did this to me in '82, I came back in '94, and now they've done it to me again. After I take out child support and other things, I'll be down to $150 a week on their [unemployment] plan. I can't live on that."</P>
<P>The UAW contract forbids plant closures until after the expiration of the agreement in September 2003. But the layoffs will start mounting immediately.</P>
<P>At the Edison plant, the evening shift will be eliminated February 4. Also scheduled for closure are the Explorer plant near St. Louis, the Cleveland Aluminum Casting Plant and Vulcan Forge in Detroit. In Ohio, the Avon Lake assembly plant will get no new models, which will eventually lead to a shutdown.</P>
<P>Ford also plans to sell a forging plant in Woodhaven, Mich. Other plants will see layoffs, including the Wixom assembly plant and Wayne stamping plant in Michigan. Even facilities that are considered "winners" in the vicious downsizing will see cuts in production.</P>
<P>"We're supposed to take a 10 percent cut in production," said R.C. Clay, president of UAW Local 551, which represents workers at the Ford assembly plant in Chicago. "We're trying to do that without having anybody laid off, so that the younger people can keep their jobs."</P>
<P>Shamefully, the UAW International leadership is rolling over for these cuts. "The UAW's history of using constructive relationships with employers to get through tough times has been proven many times over," UAW President Steve Yokich declared after the announcement. "Despite its current problems, Ford is a fundamentally healthy company, and we are confident that it will be around for a long time to come."</P>
<P>Ron Gettelfinger, head of the Ford Department and the union leadership's candidate for president, promised only to "enforce the contract."</P>
<P>By contrast, Canadian Auto Worker (CAW) President Buzz Hargrove denounced the closures and threatened to strike over Ford's plan to shutter its F-Series truck plant in Oakville, Ontario. "The only weapon workers have&#133; in bargaining is our right to withhold our labor," Hargrove told reporters. "We will go to the bargaining table with Ford Motor Co. with a strike deadline, with one of our key demands being a continuation of the operations of Oakville Truck."</P>
<P>Hargrove and the CAW are right. Unless the UAW stands up to these layoffs, more job cuts at Ford--as well as at General Motors and DaimlerChrysler--will hit soon.</P>
<P>It's time for UAW members across the industry to organize to defend their jobs--and to put pressure on union officials to stop "cooperating" and put up a real fight.</P>

