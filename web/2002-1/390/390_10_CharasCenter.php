<P><font face="Times New Roman, Times, serif" size="5"><b>Charas Community Center</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Petrino DiLeo</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 18, 2002 | Page 10</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--After a three-year fight, activists in Manhattan's Lower East Side lost a vital organizing space and community resource when the Charas/El Bohio Community Center was shut down December 27 by more than 50 police officers callously enforcing a court-ordered eviction.</P>
<P>Charas--which has served as the home for activist meetings, free classes for members of the community and low-cost studio space for artists for more than 22 years--had been the target of a long campaign by the city and local developer Greg Singer to turn the former school into luxury condos. </P>
<P>Local activists began using the center after the city abandoned the space in the late 1970s. But in 1998, the site was sold out from under the community for more than $3.15 million. The effort is part of a larger trend in the neighborhood that has resulted in the systematic destruction of community garden space and displacement of longtime residents. </P>
<P>Seven activists--part of a group that spent two days and nights in the building--were arrested and charged with resisting arrest and interfering with government administration after they had chained themselves together in one of the building's rooms.</P>
<P>The eviction was staged in a way to confuse activists, explained Mike Casey--one of the seven arrested. Police had said the notice would be served first thing in the morning either Thursday or Friday after Christmas, Casey told <I>Socialist Worker</I>. </P>
<P>After hundreds of activists gathered to defend the center on Thursday, nothing happened for several hours. "Some people had to get to work and left, and then the cops came out of nowhere," Casey said. "It was like they were hiding in the building already."</P>
<P>More than 50 police in full riot gear carried out the raid.</P>

