<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Saying no to the IMF's bitter pill</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 18, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"IF I rob a bank, they throw me in jail. But if they rob me, then they say that's okay." That's how one demonstrator described the Argentine government's currency devaluation and extension of a freeze on bank accounts, implemented last week.</P>
<P>The measures will hit hard in a country where unemployment is 20 percent, and 40 percent of the population lives in poverty. But adding insult to injury, the government set aside $26 billion of the money confiscated in the freeze on bank accounts to compensate foreign multinationals for investment losses caused by the devaluation.</P>
<P>The devaluation sparked new protests after several weeks of relative quiet since mass demonstrations toppled two governments at the end of December.</P>
<P>But ask the International Monetary Fund (IMF), and they'll say that the Argentine government hasn't gone far enough. "One also must recognize that without pain, [Argentina] won't get out of this crisis," IMF deputy managing director Anne Krueger said last week.</P>
<P>Ordinary Argentinians rejected the bitter pill of austerity in December when they took to the streets against a government that did everything the IMF asked. This mass rebellion showed the potential for an alternative to the free-market program set in Washington and carried out by governments around the world.</P>
<P>The discontent with these policies will be seen in New York City later this month when thousands come to protest the meeting of the World Economic Forum (WEF). Every year, the world's most powerful corporate bosses and government officials come to the WEF to eat, drink and promote the latest schemes of corporate globalization.</P>
<P>The conference was moved from the posh Swiss resort of Davos as a "show of support" for New York after the September 11 attacks. But activists plan to expose the WEF's real agenda. </P>
<P>They got welcome news earlier this month when the AFL-CIO said it was joining the protests. The federation is planning a January 31 town-hall meeting to "focus on the impacts of globalization and the global recession on workers in specific sectors as well as the effects of our country's recession on workers in the public and private sectors."</P>
<P>That will be followed by a march led by the UNITE garment workers union. A demonstration is also planned for February 2, and students will hold a countersummit at Columbia University.</P>
<P>The struggle in Argentina and the spreading Enron scandal have the potential to reinvigorate the global justice movement. This is our chance to take the fight against corporate globalization to the belly of the beast.</P>

