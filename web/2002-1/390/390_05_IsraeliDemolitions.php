<font face="Times New Roman, Times, serif" size="4">"We heard the bulldozers, and we ran"</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Sharon's latest war crime</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By John Buttell and Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 18, 2002 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>A "DEFENSIVE" measure. That's what Israeli Prime Minister Ariel Sharon dared to call his government's latest war crime. Last week, Israeli troops invaded the town of Rafah in Gaza and demolished more than 50 houses, leaving more than 600 people homeless, according to the International Committee of the Red Cross.</P>
<P>Residents were given no notice. They barely had time to scramble outside as Israeli soldiers, escorted by tanks and under the cover of gunfire, bulldozed the houses. "I heard the bulldozers and tanks, and we ran away into the night," 8-year-old Maryam Ghneim told reporters. "My father held me, and we were crying."</P>
<P>Tragically, the experience of homelessness is a familiar one for Palestinian families--almost all of the people of Rafah are refugees from the ethnic cleansing that accompanied the creation of the state of Israel in 1948. </P>
<P>In the same attack, Israeli troops destroyed the civilian airport in Gaza, cut the main road between Rafah and Khan Younis and destroyed the Khan Younis sewage system. Even Zeev Schiff, military analyst for the mainstream Israeli newspaper <I>Ha'aretz,</I> denounced the destruction as "an act of undisguised ruthlessness, a military act devoid of humanitarian and diplomatic logic."</P>
<P>But Israeli Foreign Minister Shimon Peres--considered the leading "dove" in Sharon's cabinet--was less critical. The "destruction of homes causes us very bad media damage," Peres admitted. "In the matter of destroying homes, we have to be very, very careful."</P>
<P>The Israeli government claimed that demolishing the homes was in retaliation for an attack by members of the Palestinian group Hamas on an Israeli army outpost. Sharon used the attack to continue his offensive against Yasser Arafat and the Palestinian Authority, claiming the attack was further proof that Arafat can't "provide security for Israel."</P>
<P>In reality, Hamas resumed its military operations in part as a way to force Arafat to back off from his policy of arresting Palestinian resistance leaders whenever Israel demands it.</P>
<P>As usual, U.S. officials barely noticed the latest atrocity committed by its closest ally in the Middle East. "We have to find all those responsible and accountable for this incident," Secretary of State Colin Powell said after the Gaza invasion.</P>
<P>But Powell wasn't talking about bringing to justice the Israeli officials who ordered this latest war crime. Powell was demanding that Arafat hunt down those responsible for a ship Israeli authorities captured two weeks ago that was allegedly bringing 50 tons of weapons to the PA!</P>
<P>The U.S. mainstream media, too, have danced to Sharon's tune. For example, the supposedly liberal National Public Radio (NPR) covered the Israeli assault as an unfortunate disruption in a peaceful period. "There's been actually three weeks of relative quiet," NPR correspondent Linda Gradstein declared. "Only one Israeli has been killed in those three weeks."</P>
<P>But you could only consider those weeks quiet if the lives of Palestinians don't count. Since December 13, the Israeli army has invaded Palestinian territory more than 20 times and killed 28 Palestinians--11 of them children. </P>
<P>Throughout Gaza and the West Bank, Palestinians live in conditions of terrible poverty--thanks to the state of siege imposed by Israel. Yet while the Palestinian people suffer, the U.S. government is financing Israel's occupation. Without billions of dollars in U.S. aid, the Israeli war machine would grind to a halt. </P>
<P>In the 1980s, activists forced corporations and public institutions to stop investing in South Africa. The campaign for "divestment" was concrete support for the struggle of the people of South Africa to destroy the racist system of apartheid. </P>
<P>We need to build a movement that challenges Israel's apartheid.</P>

