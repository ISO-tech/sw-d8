<P><font face="Times New Roman, Times, serif" size="5"><b>Boycott Taco Bell</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 18, 2002 | Page 10</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>IRVINE, Calif.--Farmworkers from Florida and their supporters plan to descend on the headquarters for Taco Bell in Southern California here March 11. It will be the biggest protest yet in the struggle by the Coalition of Immokalee Workers (CIW) in their national boycott of Taco Bell to win decent wages and conditions for workers who pick tomatoes used by the restaurant chain.</P>
<P>"Taco Bell is a multinational corporation with $5.2 billion in annual sales, and is part of Tricon, the world's largest restaurant system with $22 billion in annual receipts," Lucas Benitez of the CIW told <I>Socialist Worker</I>. "Well, we as farmworkers are tired of subsidizing Taco Bell's profits with our poverty."</P>
<P>Taco Bell's leading contractor pays workers so little that they would have to haul two tons of tomatoes to make $50 a day! </P>
<P>But workers have fought back. "Five years ago the contractor would walk around with a pistol outside of his shirt," Benitez told <I>Socialist Worker</I>. "If you wanted to drink water on a hot day, you'd have to ask, and if he didn't want you to, you wouldn't. If you did, you'd get hit or beaten. In 1996 we had a march of 500 workers in Immokalee to the house of one of the bosses to stop this type of abuse. When we did that march, that type of abuse stopped."</P>
<P>Now the Immokalee workers are fighting to stop abuse by Taco Bell--and they need our support.</P>
<I><P>A national day of action to support the Immokalee workers was set for January 15 as </I>Socialist Worker<I> went to press. More information is available online at www.ciw-online.org.</P></I>

