<P><font face="Times New Roman, Times, serif" size="5"><b>Arthur Andersen helps cook the books</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 18, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>HOW DID Enron get away with its swindles for so long? Wasn't anyone--investors, bankers, accountants--keeping an eye on the books?</P>
<P>They were. But no one cared--as long as the profits rolled in. "Many Wall Street analysts&#133;admitted that they had to take the company's word on its numbers," journalist Bethany McLean wrote in <I>Fortune</I> magazine, "but it wasn't a problem, you see, because Enron delivered what the Street most cared about: smoothly growing earnings."</P>
<P>Likewise, big banks like Citigroup and J.P. Morgan Chase kept pumping out the loans. No one was going to pull the plug on Enron--not when there were massive fees to be made for arranging the deals.</P>
<P>And as for the auditors, business insiders say that they're the biggest crooks of all. Enron's auditor was Arthur Andersen, one of the country's Big Five accounting firms. Andersen charged $27 million last year for checking Enron's books. But it also took in $28 million in "management consulting fees" from Enron.</P>
<P>Put another way, as consultants, firms like Andersen help Corporate America come up with ways to cook the books. Then as "auditors," they give the financial shenanigans an official stamp of approval.</P>
<P>Isn't that a conflict of interest? Sure. But if you can get Washington to go along, it doesn't matter, does it?</P>
<P>In the summer of 2000, Arthur Levitt--then chair of the Securities and Exchange Commission (SEC), the government agency that oversees financial transactions--proposed a new rule to prevent accounting firms from being both consultant and auditor to the same company.</P>
<P>The Big Five didn't like that. So they got their servants in Congress to get the rule dropped.</P>
<P>Andersen came under fire this month when the firm revealed that a "significant but undetermined" number of Enron documents had been destroyed. The memo asking employees to get rid of anything related to the audit but "basic work papers"--in Enron's audit alone, not any other--came four days before Enron revealed to the world that it was worth $1.2 billion less than anyone knew.</P>
<P>Business commentators are claiming to be outraged. But it's standard practice for companies to destroy potentially damaging documents. "At the crux of many corporate crises, there are typically a handful of key documents," a well-known accounting industry lawyer named Harvey Pitt wrote in 1994.</P>
<P>"Corporate counsel must take every available opportunity to imbue company executives with the understanding that their documents will take on separate lives when they enter the treadmill of litigation&#133;Each company should have a system of determining the retention and destruction of documents."</P>
<P>In other words: When in doubt, head for the shredder.</P>
<P>And by the way, are you wondering what Harvey Pitt is doing today? He was appointed by George W. Bush to head the SEC--which is running investigations into both Enron and Arthur Andersen.</P>

