<P><font face="Times New Roman, Times, serif" size="5"><b>Bush's quiet war on the unions</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 18, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. Bush is sending a message to corporations that break the law: Let's do business!</P>
<P>One of his first executive decisions of 2002 was to repeal a rule introduced by Bill Clinton that bars federal contracts for businesses that violate environmental, labor and other regulations. The U.S. Chamber of Commerce was thrilled. "Government agents could have wielded virtually unlimited power under this rule," whined Randel Johnson, vice president for labor and employee benefits at the employers' organization.</P>
<P>What a joke! In reality, the federal government has a long history of rewarding nasty corporations. In one recent year, companies that were given $38 billion in federal contracts were cited with more than 5,000 violations of health and safety laws, according to an AFL-CIO study.</P>
<P>Bush's repeal of the Clinton rule was only one in a series of attacks on labor, carried out quietly while the media waved the flag over the war on Afghanistan.</P>
<P>Earlier this month, Bush appointed Eugene Scalia as the Labor Department's inspector general while Congress was in recess--meaning that Scalia will now serve for a year without being confirmed.</P>
<P>Scalia is the son of archconservative Supreme Court Justice Antonin Scalia. But he's notorious for having antiworker views in his own right. For example, he criticized ergonomics regulations that protect workers from repetitive stress injuries as "quackery" based on "junk science." </P>
<P>With "friends" like these at Bush's Labor Department, workers don't need any enemies.</P>

