<P><font face="Times New Roman, Times, serif" size="5"><b>Getting its way in Congress</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 18, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ONE CLASSIC example of Enron's political influence came in June 2000, when several lawmakers proposed the Commodity Futures Modernization Act. The legislation would have tightened government oversight of precisely those obscure energy trading markets where Enron was a major player.</P>
<P>So Enron got to work. Within three weeks of the bill's introduction, the Republican and Democratic Parties had taken in $220,000 in soft-money contributions from Enron.</P>
<P>Enron lobbyists descended on Capitol Hill. And lo and behold, the version of the legislation that passed the Senate Banking Committee had a provision exempting energy markets.</P>
<P>The chair of the Senate Banking Committee? Sen. Phil Gramm (R-Texas), whose wife was on Enron's board and who had raked in $97,350 from Enron since 1989.</P>

