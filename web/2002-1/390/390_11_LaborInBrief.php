<p><a name="Pacifica"></a><font face="Times New Roman, Times, serif" size="4"><b>Pacifica</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Wengraf</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Activists fighting to save the Pacifica Radio Network from last year's corporate-backed takeover scored a major victory at the interim Pacifica national board meeting the weekend of January 11-13. </P>
<P>The meeting was the first for the newly formed, progressive-controlled board. More than 700 people fighting corporate control at Pacifica packed the union hall and jammed the sidewalk outside for the meeting. </P>
<P>Activists won eight key resolutions, the culmination of a hard-fought battle to return free speech, union rights and community control to the five-station national network. Resolutions passed included rehiring all 300 progressive employees who've been banned and fired. The gag rule on discussing Pacifica policy on-air was also lifted. </P>
<P>Demonstrations, community forums, direct action and a national boycott of Pacifica's fund drive--which left the network nearly $3 million in debt--were all key to winning.</P>
<P>The Pacifica campaign also won with the board's appointment of former <I>Democracy Now!</I> producer Dan Coughlin as interim Executive News Director. Coughlin's return guarantees that station managers will no longer be able to keep Amy Goodman's <I>Democracy Now!</I>--Pacifica's most popular show, with 750,000 listeners--off the air. In December, a judge had ordered that the show be broadcast, but managers at WBAI in New York and KPFA in Berkeley, Calif., continued to muzzle it.</P>
<P>"This is a people's victory," <I>Wake Up Call</I> co-host Bernard White told <I>Socialist Worker</I>. "It's a victory due to the perseverance and dedication of listeners and a community struggle over a national institution."</P>

