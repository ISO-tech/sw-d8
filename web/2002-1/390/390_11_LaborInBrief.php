<P><font face="Times New Roman, Times, serif" size="5"><b>Queens bus drivers wildcat</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Dominic Renda</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ABOUT 1,400 Queens bus drivers and mechanics staged a six-hour wildcat strike January 7 against three private bus lines. Workers have been without a contract for 13 months. "The union leadership has been complacent," Dulang, a driver, told <I>Socialist Worker</I>. </P>
<P>The union is looking for parity with public transportation workers, another driver told <I>Socialist Worker</I>. Currently, the private-line drivers make about $3 less an hour than their public-sector counterparts. Workers also want job protection in the event that the city's operating agreements with the three private bus lines are given to other companies.</P>
<P>Union leaders had already announced that they didn't want a strike until after Christmas. But when December 25 passed, union officials further delayed a strike because they didn't want to appear as if they were striking a blow at the newly elected Mayor Michael Bloomberg. </P>
<P>This is a failed strategy. The union has nothing to gain by playing nice with Bloomberg--who ran on the platform that he is a businessman. "Big corporations are trying to run the lives of the people," said Dulang.</P>
<P>When workers handed out leaflets to the public, they got a lot of support, said Dulang. There's no reason to delay a strike any further.</P>

