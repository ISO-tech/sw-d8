<p><a name="immigrant"></a><font face="Times New Roman, Times, serif" size="4"><b>LA immigrant workers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Karl Swinehart</b></font> </P>
<font face="Times New Roman, Times, serif" size="3"><P>LOS ANGELES--Workers from Forever 21 and Assi Markets held a joint protest with community supporters in Koreatown January 4.</P>
<P>The garment workers at Forever 21 are fighting for thousands of dollars in overtime and back pay from their former employer, while workers at Assi Markets are fighting for a union at the nationwide Korean supermarket chain. The action brought together workers from the two most important organizing efforts among immigrant workers in LA.</P>
<P>The protest started at Take Sushi, a restaurant owned by sweatshop boss Do Won Chang. Chang owns Forever 21 and owes 19 workers hundreds of thousands of dollars in back pay and overtime. </P>
<P>Workers at Forever 21 regularly worked 10 to 12 hours a day, earning below minimum wage in dirty, unsafe factories. "There came a moment when I said to my boss that I wasn't earning enough and wanted the minimum wage--he fired me," Esperanza told <I>Socialist Worker</I>. "He said that the fact that I didn't have papers meant that I didn't have rights."</P>
<P>Forever 21 will make $400 million in sales this year and has hired one of the largest corporate law firms to fight the workers. </P>
<P>After picketing Take Sushi, protesters set down their signs and crossed the street to Assi Market, where workers are fighting to join the Immigrant Workers Union (IWU). In November, workers at Assi filed for a union election for the IWU. In response, owner Daniel Lee hired union busters Littler and Mendelson and filed an Unfair Labor Practice (ULP) against the IWU to delay the election and harass the workers. The ULP was dismissed January 3. </P>
<P>"Managers keep very close supervision and workers can be fired for any reason," Liz Sunwoo, an organizer for the Korean Immigrant Workers Advocates, told <I>Socialist Worker</I>. "When workers requested 10-minute breaks, overtime or days off, they've been fired." </P>
<P>These workers are an inspiration for the entire labor movement and need everyone's support.</P>
