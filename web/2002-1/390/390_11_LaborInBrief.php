<p><a name="Cent"></a><font face="Times New Roman, Times, serif" size="4"><b>Jack's 99 Cent Store</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Monique Jeanne Dols</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--UNITE Local 169 suffered a defeat this week when they lost their union election at Jack's 99 Cent Store. Jack's waged an all-out antiunion campaign, using dirty tactics such as firing militant workers and promoting anti-union workers.</P>
<P>Workers at Jack's are from diverse backgrounds ranging from West Africa to the Caribbean to the Middle East. Jack's played up ethnic and racial differences as part of its divide-and-conquer strategy. The company used the anti-immigrant political climate as a scare tactic to intimidate workers.</P>
<P>Three workers were fired for campaigning for the union, explained Jean from West Africa. "People are scared to death," Jean told <I>Socialist Worker</I>. "Many of us are here without papers and are supporting our families. Although we only make minimum wage and have little to lose, Jack knows he has us cornered."</P>
<P>The defeat at Jack's is a reminder of why we have to fight for immigrants' and workers' rights.</P>

