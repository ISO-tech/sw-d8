<P><font face="Arial, Helvetica, sans-serif" size="2"><b><I>SW</I> SPECIAL FEATURE</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The fall of the house of Enron</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 18, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"A HOTBED of entrepreneurial activity and an engine of growth." So said Harvard Business School professor Christopher Bartlett of the energy giant Enron a few years ago. Well, Enron sure was a hotbed of something.</P>
<P>Bartlett may feel silly today after his "engine of growth" collapsed late last year in the largest bankruptcy in history. But he won't be alone.</P>
<P>Enron was the toast of Wall Street during the 1990s. A pipeline operator turned all-around energy conglomerate, Enron became, for ideologues of the free market, a symbol of everything that was possible under capitalism--if you could just get rid of pointless government regulations and other obstacles to superprofits.</P>
<P>Today, Enron is waiting on the bankruptcy courts to decide how to dismantle it. Executives face big lawsuits from investors and former employees--and a dozen investigations, led by everyone from Congress to the Securities and Exchange Commission.</P>
<P>Meanwhile, the company's friends in Washington--who greased the wheels for Enron's rise to glory--are scrambling to distance themselves from the wreckage.</P>
<P>The fall of Enron is an old-fashioned tale of greed, deceit, power politics and common criminality. In other words, a story of the capitalist free market.</P>
<P>"The details are bewilderingly complex, but the story line is very familiar," wrote Robert Kuttner in <I>American Prospect</I> magazine. "The watchmen are tamed, bedazzled or bribed, while a pitchman who claims to have invented something brand new in the history of capitalism takes everyone to the cleaners."</P>
<P>Here, ALAN MAASS and TODD CHRETIEN explain why Enron collapsed.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<P><A HREF="390_06_EnronTaleOfGreed.shtml"><font face="Times New Roman, Times, serif" size="4"><b>A tale of greed, deceit and power politics</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">When Asia was hit by a desperate financial crisis in 1997, U.S. business commentators blamed crony capitalists. Capitalists in South Korea and other Asian countries hid financial problems, and they were all in bed with governments and the banks. But now we know crony capitalism, U.S.-style--and its name is Enron.</P>

<P><A HREF="390_06_AndersenCooksBooks.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Arthur Andersen helps cook the books</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">How did Enron get away with its swindles for so long? Wasn't anyone--investors, bankers, accountants--keeping an eye on the books? They were. But no one cared--as long as the profits rolled in.</P>

<P><A HREF="390_06_EnronExecsCashIn.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Executives cash in as workers take the hit</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">Even as they hyped Enron as the company to lead American capitalism into the 21st century, company executives were selling, selling, selling.</P>

<P><A HREF="390_06_EnronWashPals.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Enron's many friends in Washington</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">"Conversation is routine between people in this country and government," sniffed White House spokesperson Ari Fleischer earlier this month. But the conversation does get easier if you spend more money on George W. Bush's political career than anyone else.</P>

<P><A HREF="390_06_EnronBuysCongress.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Getting its way in Congress</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">In June 2000, when several lawmakers proposed the Commodity Futures Modernization Act, which would have tightened government oversight of precisely those obscure energy trading markets where Enron was a major player, the company got to work.</P>

