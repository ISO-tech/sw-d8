<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Rumsfeld's idea of justice</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 18, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>YOU COULDN'T get away with treating animals this way. But according to Defense Secretary Donald Rumsfeld, the physical and mental abuse of suspected al-Qaeda prisoners is standard operating procedure for the U.S. military.</P>
<P>Last week, 20 people captured in Afghanistan and turned over to the U.S. military were herded aboard a plane for a 20-hour flight to a prison camp in Guant&aacute;namo Bay, Cuba. Their hands and feet were shackled, and their heads were hooded for the entire flight. At least one was drugged.</P>
<P>In Cuba, the men are now housed in cages: 6-by-8-foot chain-link enclosures, with concrete floors and metal roofs, under the constant glare of floodlights. "The detention compound, known as Camp X-Ray, resembles a Second World War prison camp," wrote Britain's <I>Daily Telegraph</I> newspaper.</P>
<P>Why is the Bush administration transporting prisoners from its war in Afghanistan to Cuba? Holding prisoners in Cuba sends the message that the U.S. military will do whatever it wants, wherever it wants.</P>
<P>And it allows the Bush administration a path toward the use of military tribunals. By law, prisoners tried in U.S. courts would have the right to appeal--but those tried at Guant&aacute;namo won't, even if sentenced to death.</P>
<P>Human rights groups, including Amnesty International and the Red Cross, are protesting the vile conditions at Camp X-Ray. But Rumsfeld wants to expand the facility--to hold up to 2,000 war prisoners in the coming months.</P>
<P>And as if a prison camp in Cuba wasn't enough, the Justice Department announced plans to round up and deport 6,000 illegal immigrants--based solely on their Middle Eastern backgrounds.</P>
<P>Attorney General John Ashcroft claims that this isn't racial profiling. But even officials from his own Justice Department are admitting the truth. "Profiling is not a four-letter word," one told the <I>New York Times</I>. "Prioritizing by looking at males who come from countries where there is support for al-Qaeda is common-sensical."</P>
<P>Torture and racial profiling--these are the sick realities of the U.S. "war on terrorism."</P>

