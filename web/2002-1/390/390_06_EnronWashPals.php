<P><font face="Times New Roman, Times, serif" size="5"><b>Enron's many friends in Washington</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 18, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"CONVERSATION IS routine between people in this country and government," sniffed White House spokesperson Ari Fleischer when asked about Enron's political influence in the Bush administration. But the conversation does get easier if you spend more money on George W. Bush's political career than anyone else.</P>
<P>From his first run for governor of Texas, Bush and Enron were inseparable--as inseparable as the company is from the Bush White House today. Dozens of leading administration officials were Enron stockholders, and several went to Washington last January directly from the Enron payroll as executives or consultants--among them, Bush's chief economic adviser Lawrence Lindsey, Federal Trade Representative Robert Zoellick and Secretary of the Army Thomas White.</P>
<P>Bush himself had the gall to try to distance himself from Ken Lay--pretending that the Enron founder had been a supporter of Ann Richards, his predecessor as Texas governor. But Bush and Lay go back much further.</P>
<P>A former cabinet member in Argentina remembers Dubya in 1988--then the beer-swilling idiot son of the vice president--lobbying him to give Enron an oil pipeline deal worth $300 million. And Bush certainly knew Lay during his father's presidency, when Enron became an increasingly generous and powerful player in Washington. In his races for governor, Bush got much more of Enron's money than anyone else--and showed his gratitude in office by supporting energy deregulation.</P>
<P>When Bush took over the White House, Lay got veto power over the new administration's energy policy--and he wasn't shy about using it. Curt H&eacute;bert, the Clinton-appointed chair of the Federal Energy Regulatory Commission, says that Lay told him he would lose Enron's support--and therefore, his job--if he wasn't sufficiently enthusiastic about deregulation. H&eacute;bert wouldn't back down and was canned. </P>
<P>His replacement, Pat Wood, sat on the Texas Public Utility Commission--in other words, he was a Texas oil insider. Wood showed his leanings last month when he declared that Enron's collapse "doesn't seem to be tied too much to deregulated energy markets."</P>
<P>Enron obviously had a lot of influence over the White House's special task force on energy issues, run by Dick Cheney. Cheney admitted this month that he or his staff met six times with Enron executives--the last one coming just a week before October 16, when Enron disclosed the depth of its problems.</P>
<P>A few weeks later, Enron executives were trying their pull with Treasury Secretary Paul O'Neill, Commerce Secretary Don Evans and others--essentially begging for the administration to intervene with banks to keep the loans coming.</P>
<P>The ties between Enron and the White House are plain to see. But as sordid as they are, they're only part of the story. An incredible 71 senators--including 29 Democrats--have taken money from Enron, as have 188 members of the House, among them 71 Democrats.</P>
<P>All told, Enron and its executives spent more than $2.4 million on national races in Election 2000. Republicans got most of the money. But Democrats got more than a quarter of it, according to the Center for Responsive Politics.</P>
<P>And campaign funding tells only a part of the story. Enron ran one of the most aggressive lobbying operations in Washington, spending $2 million last year alone. Its list of pitchmen ranged from former Christian Coalition head Ralph Reed to Republican National Committee Chair Marc Racicot to Jack Quinn, former White House counsel to Bill Clinton.</P>
<P>If Enron remained primarily associated with the Republicans, its lobbyists knew how to get what they wanted from Democrats, too.</P>

