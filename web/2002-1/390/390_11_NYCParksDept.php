<P><font face="Times New Roman, Times, serif" size="4">New York City plans to fire 3,500 workfare workers</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Making money off welfare</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 18, 2002 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WITH CHARACTERISTIC cruelty, the last act of New York City's outgoing welfare chief Jason Turner was to fire 3,500 former welfare recipients working for the Parks Department. </P>
<P>Successive mayors have hammered the Parks Department. In 1986, 4,900 people worked in the department--today only 1,900 full-time union positions remain. Now a private company, Randstand, will supply temporary employees for these positions--for a tidy $578 million. </P>
<P>Workers who were earning $9.38 per hour in union jobs can now be rehired for the same jobs at $7.95 per hour with no benefits or job security. Randstand stands to make $80 million on the deal. </P>
<P>In addition, the city told 1,200 people not to apply for temporary assistance from the state by luring them with jobs in the Parks Department that they knew did not exist. Now, people reaching their five-year limit on welfare who could have received Safety Net Assistance are without jobs or welfare benefits. </P>
<P>Here, MARK ROSENTHAL, president of DC 37 Local 983, speaks to <I>Socialist Worker</I>'s AMY MULDOON about their situation.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><B><P>HOW DID the Parks Department and the union fare under outgoing Mayor Rudy Giuliani? </P>
</B><P>HE CUT the parks so much--as did his predecessor David Dinkins. There's no meat left on the bone--there's nothing left to cut. </P>
<P>We had 7,700 welfare workers working for their welfare checks, so we sued them and got it down to 1,300. Then they came up with an alternate source of federal money and rehired welfare workers with paychecks. </P>
<P>So they finally did the right thing, and then all of a sudden, Jason Turner comes up with a $578 million contract for Randstand and tells a bunch of workers to join up for the job instead of going on Safety Net. But now they're in the street. </P>
<P>And some may get to come back at $7.95 per hour, and someone makes a nice $80 million dollars in the middle. It's outrageous. </P>
<B><P>WHAT'S THE effect of the welfare-to-work program on the city unions? </P>
</B><P>WORKFARE FOR free? Terrible. It enabled them to displace civil servants and ship people out and bring in slave labor. </P>
<P>You have to realize how terrible this is--it's single mothers with children. With a layoff or firing like this, you've disrupted an entire family. It hurts the kids. </P>
<P>So you go from $1,000 a month for clothes and groceries to $170 a month cash assistance and $6 a day for food. I'd like to know if politicians like Michael Bloomberg could eat on $6 a day.</P>
<B><P>DO YOU have any plans to fight the firings? </P>
</B><P>YES, WE'RE going to mobilize. We're waiting on DC 37 head Lee Saunders to formalize a plan. The layoffs don't start until the last week of February. </P>
<P>I hope Michael Bloomberg rethinks this. The union was just getting into gear with getting them some rights, like hearing rights which they don't have, and education benefits to help them get a GED, so they can have a diploma and can move into the private sector. </P>
<P>And now they want to dismember it and put in a despicable middleman who takes $80 million in taxpayers' money?</P>

