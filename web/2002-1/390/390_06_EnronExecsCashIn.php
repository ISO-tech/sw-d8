<P><font face="Times New Roman, Times, serif" size="5"><b>Executives cash in as workers take the hit</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 18, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>EVEN AS they hyped Enron as the company to lead American capitalism into the 21st century, company executives were selling, selling, selling. According to a lawsuit filed by shareholders, 29 top executives and board members--virtually every leading figure at Enron--sold an average of 44 percent of their personal holdings of Enron stock from 1998 to 2000, to the tune of $1.1 billion.</P>
<P>Ken Lay dumped more than $101 million in stock. Former Chief Financial Officer Andrew Fastow, the architect of the scheme to hide the company's debt, sold almost every share of Enron stock he owned--for a cool $30 million.</P>
<P>Meanwhile, Enron employees were blocked from pulling their retirement savings out of company stock--even as the share price fell to less than $1. The Labor Department estimates that Enron employees lost as much as 90 percent of their retirement savings. But the crooks at the top got out while the getting was good.</P>

