<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ZIMBABWE</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"Mugabe wants to instill fear"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 18, 2002 | Page 5</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ZIMBABWEAN PRESIDENT Robert Mugabe last week pushed through laws that will criminalize his political opponents and the free press. Mugabe hopes the measures will ensure his victory in the March presidential elections.</P>
<P>He and his ZANU-PF party led the national liberation war that overthrew the old white colonial regime of Rhodesia in 1980. But his corrupt and increasingly dictatorial regime is now widely hated.</P>
<P>In recent years, Mugabe has tried to boost his popularity by encouraging landless Black peasants--many of them veterans of the war for liberation--to occupy rich farmland still owned by a tiny minority of wealthy white farmers. But this is after 20 years of silence on land reform.</P>
<P>Meanwhile, Mugabe cracked down on the Movement for Democratic Change (MDC), an opposition party with origins in the labor movement.</P>
<P>With 30 of Mugabe's appointed members of parliament providing the margin of victory, lawmakers passed the Public Order and Security Bill, which outlaws "false statements prejudicial to the state or that incite public disorder, violence, affect defense and the economic interests of the country." This bill also bans public gatherings "to conduct riots, disorder or intolerance." It is also against the law to make or publish statements that "undermine" Mugabe's authority.</P>
<P>A member of the International Socialist Organization (ISO)- Zimbabwe spoke with <I>Socialist Worker</I> about Mugabe's crackdown.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>MUGABE HAS done this for survival in forthcoming elections set for March 9 and 10. He is resorting to smashing democracy and any potential resistance. This is being done to instill fear among citizens, especially those in the rural areas--to let them know that if they vote for the MDC, there will be civil war.</P>
<P>Besides the war veterans, Mugabe has created a militia of youth. They're doing what is called "national service," but they're also receiving military training.</P>
<P>Mugabe has increased his support in rural areas by supporting land invasions by peasants. But this has given at most 120,000 people land out of 500,000 who are supposed to get it.</P>
<P>The economy continues to worsen. There are shortages of basic commodities like sugar and cooking oil, even after Mugabe imposed price controls. Inflation is 110 percent. And unemployment is 50 percent.</P>
<P>Mugabe tried to gain support from workers by introducing a minimum wage of 8,900 Zimbabwe dollars a month. The official exchange rate is one U.S. dollar to 55 Zimbabwe ones, but the unofficial rate is 1 to 250. </P>
<P>So far, there have been two strikes in the private sector. There will probably be more when industry opens January 14 [after the holiday break].</P>
<P>The MDC opposition is hoping for a protest vote. But they aren't putting forward anything concrete on the ground right now.</P>
<P>There some protests being planned to defend democracy at the end of the month. They're being organized by the Zimbabwe Congress of Trade Unions, the National Constituent Assembly, student groups and the ISO.</P>
<P>As for the sanctions proposed by the [European Union] and the Zimbabwe Democracy Bill in the U.S. Congress, we don't support them. They're not going to liberate the people in Zimbabwe in any way.</P>
<P>America is not a friend of democracy, given the war that has taken place in Afghanistan today and Vietnam and Southern Africa in the past. Western countries are pushing more of the neoliberal policies that ZANU-PF first adopted in 1990. Those policies are what led to the crisis today.</P>
<P>We're saying that the people of Zimbabwe should liberate themselves.</P>

