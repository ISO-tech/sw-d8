<P><font face="Times New Roman, Times, serif" size="5"><b>Supreme Court's insult to the disabled</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 18, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WORK ISN'T a "major life function." That's what the U.S. Supreme Court told workers last week in a unanimous ruling that will make it harder for workers to qualify as "disabled" and force employers to accommodate their disabilities.</P>
<P>Striking a blow against the 1990 Americans with Disabilities Act (ADA), the unanimous ruling centered on the case of Kentucky autoworker Ella Williams. Williams developed carpal tunnel syndrome--a repetitive-strain injury that causes pain in her hands, wrists and arms--as a result of her assembly-line job at a Toyota factory.</P>
<P>Initially, the company agreed to give Williams work that didn't require her to use her arms, such as inspecting car finishes. But management later assigned her additional work that made her injury worse. When Williams began missing work because of her condition, Toyota fired her.</P>
<P>A lower court ruled that Williams qualified as disabled--as long as she had a medical diagnosis showing that she couldn't perform manual functions required in her job. But Supreme Court Justice Sandra Day O'Connor thinks this definition is too easy. "The central inquiry must be whether the claimant is unable to perform the variety of tasks central to most people's daily lives," O'Connor wrote in her opinion.</P>
<P>What kinds of tasks? "Household chores, bathing and brushing one's teeth are among the types of manual tasks of central importance to people's daily lives," she wrote. Unbelievably, O'Connor claimed that duties at work aren't "an important part of most people's daily lives." Well, maybe not for Supreme Court justices.</P>
<P>Williams' case was sent back to a lower court for a ruling, but the damage is done. Marca Bristo, chair of the National Council on Disability, said that the decision "would prevent many individuals whom Congress intended ADA to cover from receiving its protection that they may need to secure and maintain employment."</P>
<P>The justices have handed a huge gift to bosses, who will find it even easier to skirt workers' concerns about safety, especially ergonomics. "What's needed in the workplace is action to prevent these injuries from happening, before workers are disabled by them," said Peg Seminario, director of safety and health for the AFL-CIO. "Because the ergonomic rule was repealed, workers have no protection. Right now, her [Williams'] employer has no legal obligation under OSHA to take action to prevent more workers from being injured or disabled."</P>

