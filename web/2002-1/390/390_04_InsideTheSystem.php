<font face="Times New Roman, Times, serif" size="5"><b>Eternal Debt, the board game</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"DO YOU dare to defeat the IMF?" That's the slogan of the newest board game to hit Argentina's toy shelves. The game, produced in a suburb of Buenos Aires, pits players against the global financial institutions.</P>
<P>The owner of the company that produces the game, Raul Ruibal, would like to expand operations and start exporting the game. But borrowing money now would be suicide. "We were thinking of building a new single factory with a government credit," Ruibal said. "But the situation here is so difficult that we are frightened of getting a loan we cannot pay...Today, maintaining one's workers and paying their salaries is considered a success."</P>
<P>Maybe players of the game have an easier time "defeating the IMF" than the Argentine government.</P>
