<P><font face="Times New Roman, Times, serif" size="4"><b>CNN: Just a little stupid</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CNN HAS a new advertising ploy. Publicize the "sexiness" of its female news anchors.</P>
<P>In early January, the network was forced to pull an ad for anchor Paula Zahn's morning show. In the 15-second ad, a narrator asks, "Where can you find a morning news anchor who's provocative, super-smart [and] oh, yeah, just a little sexy?"</P>
<P>"CNN, yeah, CNN," comes the answer." During the spot, the words "provocative," "smart" and "sexy" are flashed on-screen, and the sound of a zipper is heard.</P>
<P>CNN apologized immediately to Zahn for undermining her credibility. But Fox News host Bill O'Reilly didn't see what the fuss was about. "I'll take [being called 'sexy'] all day long," he said. "You see, a person can be sexy and intelligent at the same time."</P>
<P>O'Reilly didn't say if anyone thinks that he is both sexy and intelligent.</P>
<P>--<I>Washington Post,</I> January 9, 2002</P>

