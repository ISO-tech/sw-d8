<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Will Colombia be the next target in the "war on terrorism"?</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>They want a war without end</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 18, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>NO COUNTRY is safe from Washington's savagery. George W. Bush's war on Afghanistan caused thousands of civilian deaths, put a gang of corrupt warlords in charge of the country and left some 1.5 million people on the edge of starvation.</P>
<P>But there's more to come. The U.S. government's "war on terrorism" could now move to the other side of the globe. Colombia's death-squad government walked away from peace talks last week and moved 12,000 troops to the border of a region controlled by guerrillas from the Revolutionary Armed Forces of Colombia (FARC).</P>
<P>The threat of a bloodbath may have been averted by a last-minute deal as <I>Socialist Worker</I> went to press. But an all-out war is still possible as the latest stage of the Colombian government's four-decade-old dirty war against its rebel opponents.</P>
<P>The U.S. government was eager for this war before September 11. In the past two years, the U.S. has funneled $1.3 billion in aid to Colombia--most of it directly to the military and police. U.S. officials claim they are fighting a "war on drugs." But this is a cover for pursuing a war on the FARC, which the U.S. has denounced as "terrorist."</P>
<P>In fact, Colombia's military and the right-wing death squads it arms and controls are the real "terrorists." These murderers have the worst human rights record in the Western hemisphere--and now their full brutality could be unleashed under the umbrella of the U.S. government's worldwide "war on terrorism."</P>
<P>The tentacles of that war threaten to spread everywhere. Earlier this month, some 650 U.S. military personnel, including 150 Special Forces troops, shipped out to the Philippines to join 1,200 government soldiers in "observing" Abu Sayyaf guerrillas, who are also on Washington's hit list.</P>
<P>Deputy Defense Secretary Paul Wolfowitz recently revealed his eagerness to go after supposed al-Qaeda supporters in Indonesia. And last month, a senior German official told reporters following a briefing by Defense Secretary Donald Rumsfeld that U.S. action against Somalia was not a question of "if" but "how and when."</P>
<P>Bush even warned Iran--supposedly an ally in the U.S. government's "coalition against terror" forged after September 11--when a U.S. official claimed that its government was providing refuge to al-Qaeda militants. And of course, Iraq--the target on the lips of every hawk in Washington since September 11--is always near top on the list.</P>
<P>If the next target isn't yet clear, one thing is: 2002 will be a "war year," Bush declared last month. "Our war against terror extends way beyond Afghanistan," he declared. "And at some point in time, maybe some president will come and say: 'You have the expertise we don't. Would you mind, maybe, have some of your troops with ours?' And the answer is: 'You bet.'"</P>
<P>The Bush crowd wants an open-ended war--with the U.S. government as the world's cop, wreaking its own brand of terrorism anywhere it wishes.</P>
<P>We can't let them carry out a new round of atrocities. Everyone who said no to Bush's war on Afghanistan needs to keep up the fight--and organize the antiwar opposition in our communities, our unions and our schools.</P>

