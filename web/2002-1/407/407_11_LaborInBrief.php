<P><font face="Times New Roman, Times, serif" size="4"><b>Los Angeles Unified School District</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sarah Knopp, UTLA member</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LOS ANGELES--The Los Angeles School Board imposed severe cuts on the 735,000-student school district on April 30, attacking teachers and students.</P>
<P>In response to a budget crisis caused in part by the public bailout of power companies last year, state officials told the Los Angeles Unified School District (LAUSD) to cut at least $428 million from its budget.</P>
<P>The effects will be devastating. Students in grades four through 12 will see two extra students in their already overcrowded classrooms next year. Programs such as special education will see an increase in student-to-teacher ratio and a reduction of aides.</P>
<P>Teachers--members of United Teachers Los Angeles (UTLA)--are under attack. About $52 million was cut from the health and welfare benefits of LAUSD employees, and teacher salaries could be frozen. This comes after teachers took a 10 percent pay cut in 1992!</P>
<P>And the State Board of Education will be auditing 10 of the school district's "worst-performing" schools. But the board's idea of "reform" is to force teachers to accept "loyalty oaths" and dress codes.</P>
<P>UTLA President-elect John Perez promised that the union would fight--but has done little so far. But teachers are organizing. More than 100 UTLA members picketed the school board meeting last month to protest the cuts.</P>
<P> "We need to start talking about the possibility for real social change," Ted Webber, a teacher at Logan Elementary, told <I>Socialist Worker</I>. "We need to start mobilizing." </P>
<P>Some 50 teachers at Mount Vernon Middle School have already held pickets to protest the loyalty oath and dress code. About 250 teachers at Roosevelt High School are refusing to sign the oath.</P>
<P>Teachers and students at the 10 schools targeted for reform plan a protest May 31. They say cut back--we say fight back!</P>

