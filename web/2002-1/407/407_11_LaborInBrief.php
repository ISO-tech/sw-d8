<p><a name="Vermont"></a><font face="Times New Roman, Times, serif" size="4"><b>University of Vermont</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>BURLINGTON, Vt.--Chanting "Hey, hey! Ho, ho! UVM has got the dough!" 75 union members at the University of Vermont (UVM) rallied for a living wage. "We want them to pay us what we're worth," said maintenance worker and negotiating team member Tom Stout.</P>
<P>United Electrical (UE) Local 267, which represents maintenance, custodial and other workers at UVM, sponsored last week's rally to kick off contract negotiations. Workers say that they won't settle for less than a livable wage.</P>
<P>A livable wage in Vermont for a single person with no children is estimated at just under $11 an hour, but most UVM workers start at about $7 an hour. "I know people who have to collect empty cans and bottles in the dorms just to make ends meet," said chief steward and custodial worker Norma Sprague.</P>
<P>UE organizer Heather Riemer noted that some staff couldn't be present for the noon rally because they work a second full-time job. "They're working their first-shift job before coming here for their second-shift job," she explained.</P>
<P>The city of Burlington and another local college have enacted livable wage standards, and UVM can afford to pay a living wage, too. In fact, a recent financial audit completed for the faculty union shows that UVM has socked away around $100 million in unrestricted savings accounts with names like "liquid plant assets." And UVM posted a net income in 2001 of $24.1 million.</P>
<P>Stout said he's watched in recent years while university administrators have given themselves 8 and 9 percent raises while staff members get 1.5 percent--or less. But that won't work this time--UVM can't claim it has "budget woes" while operating at a profit!</P>
<P>"We will stand up and get it!" Stout told the cheering crowd.</P>

