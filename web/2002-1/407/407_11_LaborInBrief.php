<p><a name="Parcel"></a><font face="Times New Roman, Times, serif" size="4"><b>United Parcel Service</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>FOR FEEDER drivers at United Parcel Service (UPS), the most important issues in ongoing contract negotiations are subcontracting, double breasting and UPS's use of railroads. When the current contract expires July 31, feeder drivers are looking for improvement in all three of these areas.</P>
<P>During the pre-Christmas peak season, UPS bosses have a free hand to use nonunion subcontractors to move trailers from city to city whenever there is a volume "emergency." What "emergency" really means is that UPS keeps profits up by using lower-paid nonunion carriers rather than hiring more feeder drivers to drive UPS trucks and earn Teamster benefits.</P>
<P>UPS feeder drivers pulling double trailers earn as much $24.06 per hour and $36 an hour for overtime with company-paid medical benefits and company payments into the union pension. Most nonunion drivers earn $15 to $20 per hour without any overtime rights and must make their own contributions into 401(k) plans and pay for their own health care.</P>
<P>UPS is notorious for double breasting--the practice of a unionized parent company opening a nonunion shop. UPS owns and operates UPS Logistics Group, which handles freight not traditionally processed in the UPS system and runs parts warehouses for auto companies, among other things. UPS Logistics Group employees are nonunion, but UPS Logistics Group trailers are often loaded and unloaded by Teamster members at UPS hubs.</P>
<P>If UPS logistic operations were unionized, it would mean thousands of new Teamster members, and it would open up new job opportunities to feeder drivers.</P>
<P>Feeder drivers are really seeing red over the number of UPS trailers loaded on flat cars and shipped cross-country by railroad when they are experiencing layoffs, or on-call feeder drivers who are working one or two days per week. Every 48-foot trailer or two pup trailers transported by rail represents 2-10 feeder drivers who won't get work. "UPS should be forced to pull volume off the rail as long as there is one feeder driver on layoff," said a driver at a recent contract meeting.</P>
<P>Elimination of rail would create hundreds of feeder driving jobs across the country and create new openings for package car drivers and give other inside workers an opportunity to move up.</P>
<P>Subcontracting feeder work is a threat to every Teamster member at UPS. This contract needs to make subcontracting a thing of the past.</P>

