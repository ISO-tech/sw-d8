<P><font face="Times New Roman, Times, serif" size="5"><b>We should celebrate the rebellion in LA</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 17, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Jim Tapscott, in response to my article on the Los Angeles Rebellion (<I>SW,</I> May 10), is breathtaking in his outrageous condemnation of one of the most heroic examples of oppressed workers--Black, white and Latino--fighting back against racism, poverty and police violence.</P>
<P>Tapscott mimics the right-wingers of the time who tried to portray the rebellion as a race riot, where whites were attacked by "mob violence." But Jim chooses to ignore that the LA Rebellion was as much an explosion against poverty and class inequality that all workers were victimized by as it was a rebellion against racist police violence against African Americans.</P>
<P>With the exception of the attack on white truck driver Reginald Denny--who, by the way, said in court that he understood the rage of his Black attackers--it's a lie to claim that there were "dozens of attacks on non-Black working-class people in Los Angeles alone." If there had been, images of the victims would have been paraded across every television network in America.</P>
<P>While Jim spews venom at those who rebelled in Los Angeles--and those of us who proudly defended their right to do so--it's pathetic that he has nothing to say about the racism, poverty or well-known and well-documented police corruption and brutality that are universally accepted as the reasons behind the riots in the first place. </P>
<P>Far from workers being "horrified at the lack of class consciousness and basic decency of [rioters]," the overwhelming majority of both Black and white workers supported the rioters, as evidenced by poll after poll.</P>
<P>Lastly, it's not the job of revolutionaries to stand in judgement of the tactics the oppressed use to fight their oppression. Rather, we argue what Malcolm X argued--that the oppressed have the right to fight that oppression "by any means necessary."</P>
<B><P>Keeanga-Yamahtta Taylor,</B> Chicago</P>

