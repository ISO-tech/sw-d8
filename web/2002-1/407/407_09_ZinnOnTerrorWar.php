<P><font face="Times New Roman, Times, serif" size="5"><b>Zinn's powerful voice against war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Elizabeth Terzakis</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 17, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>BOOKS:</B> Howard Zinn, <B><I>Terrorism and War.</B></I> Seven Stories Press, 2002, 159 pages, $9.95.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>SINCE SEPTEMBER 11, the media has dedicated itself to dishonesty and spinelessness in its support of George W. Bush's "war on terrorism."</P>
<P>Need some air? Then pick up a copy of <I>Terrorism and War,</I> a collection of interviews with Howard Zinn by editor Anthony Arnove.</P>
<P>"There is a precise division between who we bomb and who we do not bomb. The division has nothing to do with which countries are harboring terrorists," notes Zinn. "The division has only to do with which countries we don't control yet. The countries that we control, like Turkey and Saudi Arabia, can harbor as many terrorists as they want. We will look elsewhere."</P>
<P>Zinn argues that, if provided the facts about civilian casualties, many people can be convinced to oppose the war. And even broader possibilities exist by linking the war abroad to the war on workers at home.</P>
<P>While Zinn's treatment of the Bush administration is scathing, so too is his criticism of the Democrats. Instead, <I>Terrorism and War</I> puts its faith in the power of ordinary people.</P>
<P>Zinn and Arnove quote activists and revolutionaries from Frederick Douglass to Eugene Debs to illustrate the fact that the U.S. has always used war and repression to achieve its ends--and that it has always been resisted.</P>
<P>In earlier essays, Zinn presents some unconvincing solutions--such as calling on the U.S. to be "a more modest nation," that no longer "[needs] to be a superpower." But by the book's end, he acknowledges that war won't end without ending capitalism.</P>
<P>"[T]he left is in a position of continually opposing war after war after war, without getting at the root of the problem--which is the economic system under which we live, which needs war and makes war inevitable," concludes Zinn.</P>

