<P><font face="Times New Roman, Times, serif" size="4">Executions halted in Maryland</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>End the death penalty now!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 17, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>IN STATES across the U.S., politicians are being forced to admit what opponents of capital punishment have long known--that the death penalty system is racist, arbitrary and punishes the poor. In short, it's a broken system.</P>
<P>On May 9, Maryland Gov. Parris Glendening called a halt to executions just one week before a death row prisoner was scheduled to be killed. That made Maryland the second state in the country where a governor has stopped executions. Two years ago, Illinois Gov. George Ryan declared a moratorium after the 13th man was exonerated and released from death row in 12 years.</P>
<P>In the meantime, states across the country have passed restrictions on capital punishment. For example, 16 states and the federal government now ban executions of the mentally retarded. </P>
<P>Racism was the chief issue in Maryland. Glendening himself ordered a study into the bias of a system where nine of the 13 prisoners on death row are African American--and all but one of the 13 are accused of killing whites, even though more than 80 percent of murder victims in Maryland are Black.</P>
<P>This pattern--of valuing white lives over Black lives--is repeated everywhere the death penalty is used. "We ought to pause, review it and make 100 percent sure that there is no error, bias or injustice in our system," Glendening declared.</P>
<P>But Glendening, who has executed two men since taking office, didn't experience this change of heart on his own. He has been under mounting pressure from death penalty opponents since the 1998 fight to save Tyrone X Gilliam.</P>
<P>That struggle, though unsuccessful, prepared the ground for mobilizations that won several stays of execution for death row prisoners--and came within a last-second parliamentary maneuver of getting a moratorium law passed by the state legislature.</P>
<P>And Maryland isn't alone. Across the country, grassroots activism has helped to focus increasing doubts about capital punishment. In Illinois, opponents of the death penalty who put pressure on Ryan to impose a moratorium gained another victory last month--the appointment of a special prosecutor to review the cases of the Death Row 10, a group of prisoners who were tortured into "confessing" by Chicago cops.</P>
<P>In California--with the largest death row in the country--activists are organizing for a moratorium. And in the death penalty capital of Texas, abolitionists are taking a stand against the state's plan to kill nine people over the next four-and-a-half months--among them, Napoleon Beazley, who was 17 years old at the time of the crime he was convicted for.</P>
<P>Our struggle is far from over. But opponents of the death penalty can take inspiration from our victories in Maryland, Illinois and elsewhere--and step up our efforts to expose the barbaric machinery of death.</P>
<P>It's time to end the death penalty--once and for all!</P>

