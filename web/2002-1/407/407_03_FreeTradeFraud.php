<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Democrats give ground on Bush's push for fast track</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Free-trade fraud</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 17, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>SHOULD THE poison come with a sugar coating or not? That just about sums up the debate on "fast track" trade legislation that was underway in the Senate as <I>Socialist Worker</I> went to press.</P>
<P>Fast track--renamed "trade promotion authority" by George W. Bush--would give the White House the power to negotiate trade deals and require Congress to vote them up or down, without amendments.</P>
<P>Under pressure from organized labor, fast track has been shot down in Congress repeatedly since 1994, when the last law granting such authority expired. Now Bush wants to renew fast track to push through the Free Trade Area of the Americas, which would put virtually the entire Western hemisphere under the thumb of U.S.-based transnational corporations.</P>
<P>To get it through the Senate, Bush had to agree to a compromise with Democrats that would extend benefits to those left jobless by trade deals. Yet even if the deal survives a vote in the Senate and a negotiating session with the House of Representatives--and that's a very big "if"--workers who qualify for the special benefits would still suffer.</P>
<P>The COBRA proposal is a joke compared to what's really needed--a national health insurance program. But even the deal that Bush agreed to is making employers nervous about "the largest expansion of government-subsidized health care in years," as the <I>Wall Street Journal</I> put it.</P>
<P>The <I>Journal </I>worried that Bush has legitimized the idea of "landmark federal entitlements for workers who lose their jobs," adding that "the program could turn out to be the first step in a nationwide unemployment assistance overhaul."</P>
<P>Translation: Workers might get the idea that they should demand unemployment benefits that actually cover everyone who loses their job, rather than just the 40 percent who are eligible today.</P>
<P>Senate Majority Leader Tom Daschle (D-S.D.) was all too willing to scrap the Democrats' original proposal for genuine health insurance coverage for retired steelworkers, whose benefits were wiped out by their companies' bankruptcy.</P>
<P>And Daschle can be expected to retreat even further in negotiations with the Republican-controlled House, where right-wingers are adamantly opposed to any benefits for workers under fast track.</P>
<P>If--or rather, when--the Democrats roll over, Daschle will try to claim that he fought the good fight. He'll say that a Democratic win in the congressional elections this fall is the only way to stand up to Bush's push for free-trade deals. Never mind that it was a Democratic Congress under Bill Clinton that passed NAFTA.</P>
<P>But with big unions like the Teamsters and Carpenters allied with the White House on key issues, labor has retreated from using the muscle it showed at the big demonstration at the 1999 World Trade Organization meeting in Seattle.</P>
<P>The only way to win real concessions for workers--let alone block fast track--is for organized labor to mobilize once more. We need a real fight for jobs--and a fight to extend unemployment benefits and provide genuine national health insurance as well.</P>

