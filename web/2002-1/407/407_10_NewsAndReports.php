<P><font face="Times New Roman, Times, serif" size="3"><b>Fight for immigrant rights</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Tim Cook</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LOS ANGELES--More than 6,000 people marched to demand immigrant rights as part of this year's May Day activities. Protesters called for an end to attacks on civil liberties and for a full amnesty for California's immigrant workers.</P>
<P>The historic turnout was the result of a large presence by rank-and-file union members and students, but most of the protesters were immigrant workers themselves.</P>
<P>That such great numbers turned out despite the Bush administration's heightened attacks on immigrants speaks to a growing anger with the government's anti-immigrant policies and an increased willingness to take to the streets.</P>
<P>All sectors of LA's diverse working class were well represented, from Korean grocery workers and Salvadoran and Mexican garment workers to a strong contingent from the Service Employees International Union, which marshaled the march.</P>
<P>Police presence was heavy throughout the day, with helicopters hovering overhead and a menacing presence along the march route of cops decked out in riot gear and mounted on horseback.</P>
<P>The march ended peacefully with a candlelight vigil in honor of immigrants "disappeared" by the Immigration and Naturalization Services in its post-September 11 witch-hunt and those who have died attempting to cross the border from Mexico.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Protest Ariel Sharon</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mali Jimenez</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WASHINGTON--Building on the momentum of the national antiwar march April 20, protesters in Washington, D.C., gathered outside the White House to speak out against Israeli Prime Minister Ariel Sharon's visit to the U.S.</P>
<P>With only a few days of organizing, members of different groups--Committee in Solidarity with the Palestinian People, International Socialist Organization, SUSTAIN, Al-Awda D.C., and the Arab Anti-Discrimination Committee--set up a picket line with about 30 protesters.</P>
<P>From there, the protest got bigger, increasing to about 150, and picketers chanted "Free, Free Palestine," and "Bush buys the bullets, Sharon pulls the trigger."</P>
<P>The protest then moved to the Omni Shoreham Hotel, about a mile away from the White House, where Sharon was speaking to the Anti-Defamation League, a staunchly pro-Israel organization based in Washington. </P>
<P>Here the crowd grew to 300, while inside the hotel, Sharon declared that Israel would not allow itself to be judged by other nations and exposed to "needless" investigation by international bodies--referring to the United Nations team slated to investigate Israeli atrocities committed in Jenin, until it was scrapped with the help of the U.S.</P>
<P>Given the looming invasion of Gaza by Israel's military, it's crucial that rallies like this one continue and grow larger. We need to show our opposition to Israel's colonial war--made possible by financial and military support from the U.S.</P>

