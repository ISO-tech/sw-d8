<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Who's destroying the environment?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 17, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SOME ENVIRONMENTALISTS argue that human beings have too much control over the environment. In this view, environmental catastrophes are caused by too much industrial development, which increases human exploitation, and thus the ruin of nature.</P>
<P>For Marxists, on the other hand, the problem is capitalism--the ceaseless quest for profit that ignores everything but the bottom line, which leads to poisons being dumped into the air, water and earth because it is cheap.</P>
<P>Modern technology, as it is employed by capitalist industry, is destructive. However, the development of modern technology and scientific knowledge makes it possible for humans to live harmoniously with their environment.</P>
<P>In an essay entitled "The Part Played by Labor in the Transition from Ape to Man," Frederick Engels shows a remarkable grasp of the process of human intervention into nature and its implications.</P>
<P>Both humans and animals, Engels argues, change their environment by acting on it. Animals do it more or less unconsciously as a byproduct of biological living patterns. While there are some examples of tool making in the animal world, they are rare and rudimentary. Only humans exert a systematically intentional influence on nature in order to harness its forces and materials.</P>
<P>"The animal merely uses the environment and brings about changes in it simply by its presence," writes Engels. "Man by his changes makes it serve his ends, masters it."</P>
<P>Ecologists may squirm over terms, but it cannot be denied that this constitutes one of the essential features that distinguishes humans from other species--our ability to band together and manipulate the forces of nature to our own ends through the systematic use of planning and tools.</P>
<P>In short, human beings produce their subsistence--and more beyond--rather than just find it. Human beings, unlike other animals, have a history that is based upon the changes that they wrought upon nature based on the social relations they create to work up nature to their own ends.</P>
<P>As if he expected to be misinterpreted by a forerunner of environmentalism, Engels continues: "Let us not, however, flatter ourselves overmuch on account of our human victories over nature. For each such victory nature takes its revenge on us. Each victory, it is true, in the first place brings about the results we expected, but in the second and third places it has quite different, unforeseen effects which only too often cancel the first."</P>
<P>This problem is, in the first place, a product of our incomplete understanding of the workings of nature and the effects of our actions upon them. The phenomenon becomes more acute under capitalism, argues Engels, where only "the social effects of human actions in the fields of production and exchange that are actually intended" are examined, and even here the "intended" effects are motivated solely by "the profit to be made on selling."</P>
<P>Engels then cites many examples of the destruction of the environment brought about by the boundless and blind lust for profit. "Thus at every step we are reminded that we by no means rule over nature like a conqueror over a foreign people, like someone standing outside nature--but that we, with flesh, blood and brain, belong to nature, and exist in its midst, and that all our mastery of it consists in the fact that we have the advantage over all other creatures of being able to learn its laws and apply them correctly."</P>
<P>To truly live harmoniously with nature, society must undergo "a complete revolution in our hitherto existing mode of production, and simultaneously a revolution in our whole contemporary social order," Engels writes.</P>
<P>The point is not to "leave nature alone," but to transform human social relations so that production (and the technology accompanying it) develops in a planned manner that takes into account human need--and, by extension, the care of the natural environment upon which human needs depend.</P>

