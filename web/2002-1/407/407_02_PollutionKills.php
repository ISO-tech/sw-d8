<P><font face="Times New Roman, Times, serif" size="5"><b>How pollution kills around the world</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 17, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MORE THAN 2 million children die each year--5,500 every single day--because of exposure to pollution, according to a new United Nations (UN) report. The report, titled "Children in the New Millennium," details the appalling conditions of poverty that millions of children face around the globe.</P>
<P>According to UN researchers, one-third of diseases worldwide are caused by drinking unsanitary water, eating tainted food or breathing polluted air. Some 1 billion people--a full one-sixth of the world's population--don't have access to clean drinking water, and 2.4 billion lack access to even simple latrine facilities.</P>
<P>This puts the most vulnerable--particularly children--at risk for preventable diseases such as cholera, typhoid and roundworm. Additionally, millions of children face the danger of pesticide poisoning--because they're forced to work as agricultural laborers.</P>
<P>"These disturbing figures show we have barely started to address the problem," said Carol Bellamy, executive director of the United Nations Children's Fund.</P>
<P>But don't expect any action from the Bush administration. In fact, the White House worked all-out last week to wreck yet another international meeting--a UN special session of the General Assembly on children.</P>
<P>The meeting was supposed to consider an international treaty on children's rights that every country in the world but the U.S. and Somalia has ratified. But the U.S. delegation, led by Health and Human Services Secretary Tommy Thompson, was outraged.</P>
<P>U.S. officials argued for nearly 30 hours to block any mention of abortion rights and sex education from appearing in the conference's final declarations. And, naturally, the U.S. opposed a treaty provision to bar the execution of children under the age of 18 years.</P>
<P>Nice to see the Bush gang standing up for the rights of children--to be killed by the state.</P>

