<P><font face="Times New Roman, Times, serif" size="4">Washington white power rally a warning</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Keep Nazis out of D.C.!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Bruce Cooley</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 17, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MORE THAN 200 neo-Nazis rallied outside the Israeli Embassy in Washington, D.C., May 11, in the largest white power demonstration in the U.S. in years. The Nazis came from around the country for the fifth mobilization in D.C. in a year organized by the National Alliance (NA).</P>
<P>The racists' numbers and confidence show the danger of a potential alliance between suit-and-tie Nazis like the NA and skinheads thugs who were apparently drawn to D.C. by a concert organized by the white power record label Resistance Records.</P>
<P>More than 100 antiracists demonstrated across the street from the fascists. Palestinians, Jews, Blacks and others joined together to counter the Nazis' message of anti-Semitism and racist terror--and to challenge the fascists' attempt to pretend that they support the Palestinian struggle for liberation. As one popular chant at the counterdemonstration put it: "Nazi racists always lie, they don't care if Arabs die."</P>
<P>Since September 11, the NA has posed as a supporter of Palestinians in order to spread anti-Semitic hate. At the rally, Nazis carried signs calling for an end to U.S. aid for Israel and an investigation of Israel's war crimes at the Jenin refugee camps. A few skinheads even carried Palestinian flags.</P>
<P>But the skinheads showed their true colors when they yelled, "What's that rag for, towelhead," at a Palestinian activist who was wearing a Kaffia--the shawl that has become an international symbol of solidarity with the struggle.</P>
<P>"It drives me crazy," said Isis Nusair, a Palestinian activist from D.C. "They're using the Palestinian issue for their own racism." Another protester agreed, telling <I>Socialist Worker</I>: "These groups are really about organizing terror against Blacks, Arabs, gays and lesbians and immigrants in our neighborhoods. Especially when we struggle for justice in Palestine, we have to make it absolutely clear that our fight against the racism and terror of the Israeli state is not a fight against Jews--that anti-Zionism has no alliance with anti-Semitism and that we're fighting against all racism and oppression, no matter who the targets are."</P>
<P>Counterdemonstrators agreed that the turnout for our side was too small. "People in D.C. aren't taking this seriously enough," one told <I>Socialist Worker</I>. "We need to activate more communities in D.C. and up and down the East Coast to keep this from happening again."</P>
<P>The Nazis will probably return to D.C., maybe as soon as this summer. When they do, everyone concerned with social justice--from those building solidarity with Palestinians to global justice activists and beyond--must mobilize a big presence to send a clear message: "No Nazis in D.C.!"</P>

