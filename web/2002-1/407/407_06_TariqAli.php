<P><font face="Times New Roman, Times, serif" size="4">Tariq Ali on pro-Palestinian protests</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"The mood is changing"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 17, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>LEFT-WING author and activist TARIQ ALI was in Washington, D.C., for the April 20 protests against Bush's "war on terrorism" and in support of Palestinian rights. He spoke to <I>Socialist Worker</I> about the growing struggle against Israel's war.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<B><P>WHY ARE demonstrations for Palestinian rights growing right now?</P>
</B><P>WHAT SHARON has done in the Middle East, and the resistance and sacrifices of Palestinians have brought the demonstrators out.</P>
<P>Even though the U.S. media coverage has been appalling, word has trickled out through the Internet and other alternative sources of information as to what's going on. And people are just disgusted--not just people on the left, but even liberals and people who aren't political. The fact that this goes on, and that the U.S. government supports it, is now beginning to anger people. </P>
<P>I thought the demonstration on April 20 that ended up being dominated by Palestine was very indicative of a change in mood. They can no longer use September 11 to frighten people and keep them indoors.</P>
<P>In the period after September 11, Arab Americans in particular and Asian Americans were really scared and saying that we can't come out. I've been told by lots of cab drivers in New York how they were spat on and insulted and forced to put up American flags and "God Save America" signs just to protect themselves--in shops and cabs and homes.</P>
<P>That's beginning to change as people see how Bush is using September 11 to re-map the world. The opposition which has emerged in the U.S. is, I think, one of the most significant developments in world politics--because we need to show the world that there's not one America, but two Americas.</P>
<B><P>HOW DO you see the situation developing?</P>
</B><P>THESE PROTESTS aren't going to lead to an insurrection, but the very fact that they take place is important. Campaigns have to be mounted in the cities, linking students and unions, and trying to put pressure on unions to participate in boycotts.</P>
<P>We have to treat Israel now like we treated apartheid South Africa--that's absolutely vital. And people are now ready to hear that, even though they weren't before.</P>
<P>Today, there are also protesters in Israel as well--Israeli soldiers, Israeli peaceniks who are disgusted with the way things are going. So this isn't an anti-Semitic protest--which is how the Zionists try to portray it.</P>
<P>We've seen demonstrations in the streets throughout the Middle East, and I think the crowned heads are trembling. Bush is on the top of all this and has backed Sharon to carry out massacres in Palestine, and if they now go and invade Iraq, that region will explode--I'm absolutely convinced about it.</P>
<P>They will not be able to hold back public anger.</P>

