<P><font face="Times New Roman, Times, serif" size="4">Impact of the welfare cuts</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Hunger on the rise among children</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 17, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>TO GET an idea of the terrible impact of the politicians' welfare cuts, take a look at the children in our nation's emergency rooms. According to a report released at a Baltimore medical conference last week, the number of children suffering from hunger and malnutrition who visited ERs in Boston and Minneapolis increased by 45 percent over two years.</P>
<P>The Boston University study looked at 3,000 children who came to the Boston Medical Center and a clinic in Minneapolis between 1999 and 2001. Patience Sampson, a social worker at Boston Medical, told the <I>Boston Globe</I> about a two-and-a-half-year-old boy who weighed just 21 pounds--one-third less than he should.</P>
<P>Sampson and others place much of the blame on new welfare rules that cut off benefits for many poor families, including their eligibility for food stamps. Millions of people have been thrown off the welfare rolls as a result of the 1996 welfare reform. In order to continue receiving food stamps, poor families have to reapply--facing the labyrinth that is the public aid system.</P>
<P>"It's very disturbing in this land of plenty, where some of us are never hungry, to realize how food security is a real problem for many, many families," Boston's deputy health commissioner Dr. Barbara Ferrer told the <I>Globe</I>.</P>
<P>Researchers say that they chose Boston and Minneapolis in part because they are in states that at one time had strong social safety nets. If the status of children in these states deteriorated to this extent, think of what is happening elsewhere.</P>
<P>"This should be a wake-up call to people," said Boston University's Dr. Deborah Frank, who led the study. "This says that there are canaries in our social mine, suggesting there are big things going on, and before we go on, we should scrutinize what we are doing to our children and especially our youngest children."</P>

