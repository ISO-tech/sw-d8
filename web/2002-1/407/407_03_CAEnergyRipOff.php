<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>California power rip-off exposed</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 17, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"I DON'T think there's any doubt that somebody ought to go to jail." That was the reaction of mild-mannered Senate Majority Leader Tom Daschle (D-S.D.) to the latest revelations in the Enron scandal.</P>
<P>The energy giant that went bankrupt late last year was forced to release internal memos that show how it ripped off California consumers--by manipulating the "free market" to create a power "crisis."</P>
<P>In one scam, nicknamed "Ricochet," Enron bought up electricity from California power producers, held it out of state until the lights started to flicker--and then sold it back to California at outrageously marked-up prices. In a typical transaction, traders sitting at computer terminals at Enron's Houston headquarters bought California electricity at $250 per megawatt--only to sell it back to the state for $1,250. </P>
<P>These latest revelations expose the real workings of the so-called "free market." When the energy crisis broke in California a couple of years ago, left-wing critics pointed out the obvious--that the eight or 10 largest energy companies in the country were working together as a cartel to steal more than $20 billion in jacked-up electricity prices that were passed onto consumers. </P>
<P>Under mounting pressure, Democratic Gov. Gray Davis talked tough for the cameras. Then he gave the corporate pirates everything that they could have dreamed of. Davis authorized a 40 to 60 percent hike in electricity rates, handed out dozens of fast-tracked permits to build environmentally disastrous fossil-fuel power plants, and signed 10- to 20-year contracts to buy electricity at artificially inflated prices.</P>
<P>Where did the money come from? Budget cuts--including more than $800 million from schools alone. </P>
<P>Davis, along with California's Democratic Sens. Dianne Feinstein and Barbara Boxer, is demanding a Justice Department investigation into Enron's crooked deals. Enron is obviously guilty. But the real culprits were homegrown mega-utilities like SoCal Edison and Pacific Gas &amp; Electric (PG&amp;E). </P>
<P>Last year, PG&amp;E openly played its own "Ricochet" scheme by laundering electricity from its own power plants through the out-of-state market. PG&amp;E sold power for 5 cents a kilowatt--then bought it back for more than a dollar.</P>
<P>After shipping the profits out of state, executives had the gall to howl about their "losses" in California--using this as an excuse to lay off workers, jack up rates and squeeze a massive bailout out of Davis.</P>
<P>One year later, PG&amp;E--while still technically in bankruptcy hearings--recorded its second most profitable year in history!</P>
<P>Tom Daschle is right. Somebody ought to go to jail, and Enron execs should be first in line. But Davis, Boxer and Feinstein are protecting the real California energy criminals.</P>
<P>California's energy rip-off shows the true meaning of the "free market." Corporations are "free" to steal as much as they can, while politicians are "free" to take as much campaign cash as necessary to let them off the hook.</P>
<P>Meanwhile, we're "free" to suffer the consequences. This whole system belongs in jail.</P>

