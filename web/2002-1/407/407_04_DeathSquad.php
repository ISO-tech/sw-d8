<font face="Times New Roman, Times, serif" size="5"><b>Death squad, how may I help you?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 17, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>HOW SHOULD right-wing paramilitaries clean up their brutal image? Start a complaint line.</P>
<P>The United Self-Defense Forces of Colombia (AUC), the paramilitary thugs responsible for numerous massacres in Colombia, announced recently that people who suffer "excesses" at the hands of its fighters can lodge a complaint via e-mail. That is, of course, if anyone is left alive to complain.</P>
<P>The AUC killed about 1,000 civilians in 2001 alone, according to the government. In one case, AUC members killed 27 peasants with sledgehammers and stones in the village of Chengue--after accusing them of collaborating with guerrillas. </P>
<P>But apparently, bloody massacres are bad PR, so the AUC is hoping its new complaint system will polish up its image. Senior AUC leader Carlos Casta&ntilde;o, who admits to having killed many times, says that he wants right-wing militiamen to commit fewer massacres and cut back links with drug traffickers.</P>
<P>The announcement, signed by the AUC's General Staff and posted on the AUC Web site, said it was "conscious of the difficulties and possible excesses committed by all armed groups, including our own, whose cause is just. Any accusations or complaints should be directed to Commander Adolfo Paz at the electronic address 'auc_inspeccion@hotmail.com."</P>
<P>--Reuters, May 3, 2002</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Bush with kung fu grip</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>A CONNECTICUT toy company has found a novel way to cash in on the "war on terrorism." The company, Herobuilders.com, recently debuted a line of "life-like action figures," including George W. Bush, former New York City Mayor Rudolph Giuliani, Osama bin Laden and British Prime Minister Tony Blair. For legal reasons, the dolls are identified by catchy names like "Our Hero," "The Rock" and "The Ally."</P>
<P>Company President Emil Vicale told reporters that at first he sold only dolls of "real guys who changed the world--the presidential figure, the mayoral figure and the prime minister figure."</P>
<P>He added the bin Laden doll after people asked for an "enemy figure." Smart move. So far, sales of the bin Laden dolls are running about one-third higher than sales of the Bush doll. </P>
<P>Vicale said he has sold only seven Giuliani dolls, however--and "not even one" Blair doll. </P>
<P>--Reuters, April 19, 2002</P>

