<P><font face="Times New Roman, Times, serif" size="4">Right wing on the offensive</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Israel tightens its grip</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 17, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"JUST SAY no to a Palestinian state!" That was former Prime Minister Benjamin Netanyahu's rallying cry at a meeting of leaders of the right-wing Likud Party, which currently heads the Israeli government.</P>
<P>With Israeli tanks ringing Gaza and prepared to launch an attack on a moment's notice, the Likud Party central committee passed a resolution that stated: "No Palestinian state will be established west of the Jordan River." This was an explicit repudiation of the decade-old Oslo "peace" process.</P>
<P>The watered-down statelet that Palestinians were supposed to control under Oslo would have left Israel's power mostly intact. But Israeli right-wingers have resisted even this. So Netanyahu pressed the issue in a battle over Likud Party leadership against current Israeli Prime Minister Ariel Sharon.</P>
<P>Sharon's entire term in office has been dedicated to destroying the basis for a Palestinian state--something that he accomplished in reality with the recent assault on the West Bank. But because Netanyahu isn't part of the government, he can openly pander to the growing support in Israel for wholesale "ethnic cleansing" of Palestinians from "greater Israel."</P>
<P>Sharon, on the other hand, has to stick to rhetoric that's acceptable to Israel's main source of military and political support--the U.S. government. He came up with a more diplomatic road to the same end--demanding a "major, institutional structural reform in the Palestinian Authority" before Israel allows any further progress on the establishment of a Palestinian state.</P>
<P>Sharon modeled his rhetoric on the Bush administration's plans for a "regime change" in Iraq to oust Saddam Hussein. So it was no surprise when George W. Bush's National Security Adviser Condoleezza Rice all but endorsed Sharon's repackaging of his earlier demand that Palestinian leader Yasser Arafat be driven from power.</P>
<P>"We are going to call on Arab allies, the Europeans and others to press [Arafat]," Rice said on Fox News. "We are going to be very clear that the Palestinian leadership that is there now, the Authority, is not the kind of leadership that can lead to the kind of Palestinian state that we need."</P>
<P>But don't be fooled by this talk of "reform." "There can hardly be a Palestinian in the West Bank or Gaza who is not desperate for an overhaul of Yasser Arafat's corrupt and paralyzed administration," wrote one British journalist. "But they want no part of the reforms being preached by President George Bush and Israel's prime minister, Ariel Sharon."</P>
<P>In fact, a more democratic PA would give voice to the millions of Palestinians who think Arafat got nothing in exchange for bargaining away most of Palestinians' historic demands. Israeli and U.S. officials don't want that kind of "reform." They want to replace Arafat with someone even more willing to cave to Israeli demands.</P>
<P>Meanwhile, as White House officials preach about a "reformed" PA, Israel's military continues to use indiscriminate violence against any sign of Palestinian resistance.</P>
<P>The threatened invasion of Gaza--in response to a suicide bombing last week--is in the cards. Though Sharon may have delayed the assault, the question is only when it will happen, not if. And when it does, the war crime committed in the Jenin refugee camp may look gentle by comparison.</P>
<P>Even without an invasion, the Palestinian death toll mounts by the day. Over the weekend, Israeli soldiers in Gaza shot a 14-year-old boy in his lower back--and then barred medical personnel from reaching him. "The occupation army didn't allow our ambulances to reach the bleeding boy," said Dr. Mouawiay Hasanin, chief administrator at the Shafa Hospital in Gaza. "His death occurred six hours after he was wounded. I'm sure he would have lived if the occupation forces had allowed medical rescuers access to him."</P>
<P>As residents of Gaza--one of the most densely populated places on earth--stock up on essentials to weather a long siege, it's critical that activists in the U.S. step up efforts to organize solidarity.</P>
<P>We say: Free, free Palestine!</P>

