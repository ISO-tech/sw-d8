<P><font face="Times New Roman, Times, serif" size="4">Right-wingers target pro-Palestinian instructor</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Witch-hunt in Berkeley</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 17, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"THE INTIFADA Curriculum" screamed the headline from the op-ed pages of the <I>Wall Street Journal</I> last week. The article, written by conservative hack Roger Kimball, attacked a class on "The Politics and Poetics of Palestinian Resistance," scheduled for next fall at the University of California-Berkeley.</P>
<P>It's no coincidence that the course is being taught by one of Berkeley's best-known left-wing activists, Snehal Shingavi, a member of the International Socialist Organization. Kimball's diatribe--and the flood of sneering commentary that followed it--are part of a backlash against activists who have dared to speak out against George W. Bush's "war against terror" and in defense of Palestinian rights.</P>
<P>In this case, right-wingers like Kimball and anti-affirmative action UC Regent Ward Connerly are attacking Shingavi, a grad student instructor, for being "biased against conservatives." Why? Because of a single phrase in the course description: "Conservative thinkers are encouraged to seek other sections."</P>
<P>"This isn't a ban on ideas," Shingavi says. "Any political thought is welcome in the class. It's my attempt to frame the class. I put the line in there basically to say that it's unacceptable to begin every class with a discussion about whether Palestinians have the right to resist Israel's occupation."</P>
<P>But UC officials didn't ask Shingavi first before firing off criticisms of the course. In a statement, Chancellor Robert Berdahl condemned the course description, declaring, "Classrooms must be places in which an open environment prevails and where students are free to express their views."</P>
<P>But Berdahl wasn't concerned about letting students "express their views" last month--when he suspended the activist group Students for Justice in Palestine (SJP) for holding a sit-in on campus.</P>
<P>Shingavi is a leader of the SJP and helped to organize the successful fight to get the suspension lifted. Now that the administration's attack on the SJP has failed, it looks like they've switched tactics--promising to "investigate" SJP members like Shingavi.</P>
<P>"I think that this is part of the same series of practices and policing of universities to not allow students to protest freely in defense of Palestine--and also to not allow instructors to teach freely about Palestine," Shingavi said.</P>
<P>For now, the university has removed the offending line from the course description and seems willing to allow the class--under "monitoring" from administrators. But Shingavi believes that the fight is far from over.</P>
<P>"Since September 11," Shingavi said, "not only has it been nearly impossible to critique the policies of the United States, it's been impossible to teach histories that are critical of U.S. policies. If I wasn't committed to teaching this, this would be very scary. But because I am committed, I think that it's important to fight at every possible level and from every angle for the right to teach this kind of material."</P>

