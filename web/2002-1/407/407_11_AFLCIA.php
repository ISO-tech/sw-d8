<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Is this the return of the AFL-CIA?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 17, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>UNION LEADERS routinely state their support for George W. Bush's "war on terror" and their unconditional backing of the state of Israel. And following the failed coup in Venezuela last month, it seems that labor officials may also be returning to the bad old days when their international efforts were known as the AFL-CIA.</P>
<P>The coup exposed labor's continuing ties to the National Endowment for Democracy (NED), a nonprofit foundation created by Congress in 1983. Back then, the NED was notorious for channeling funds for the U.S.-backed Contra war against the Nicaraguan government--with the enthusiastic support of then-AFL-CIO President Lane Kirkland.</P>
<P>In recent months, the NED used the AFL-CIO to funnel $155,377 to Venezuela's FTV union federation. Leaders of the notoriously corrupt FTV supported the bosses' strike in the crucial oil industry that prepared the way for the attempt to topple President Hugo Ch&aacute;vez. The FTV, which Ch&aacute;vez tried to replace by a referendum, initially supported the coup--only abandoning it after its leaders were shut out of the new regime.</P>
<P>FTV leaders had met with the NED and AFL-CIO at a closed-door forum two months before the coup--begging the question of whether Ch&aacute;vez's ouster was discussed.</P>
<P>The NED is run by what used to be called "State Department socialists"--former left-wing intellectuals who joined Ronald Reagan's anticommunist crusade. Before the mid-1990s, the NED routinely channeled money through the AFL-CIO's American Institute for Free Labor Development (AIFLD) to government-run unions in pro-U.S. dictatorships, from Central America to the Philippines under Marcos.</P>
<P>After he took over as AFL-CIO president in 1995, John Sweeney scrapped the AIFLD and replaced it with the American Center for International Labor Solidarity (ACILS), run by labor progressive Barbara Shailor. The ACILS carried out work unthinkable in the Kirkland era--supporting democratic, independent unions in Mexico, for example.</P>
<P>Nevertheless, the NED still counts the ACILS as one of its "four core institutes"--alongside one sponsored by the Republicans, one by Democrats and one by big business.</P>
<P>Will the AFL-CIO come clean? Stan Gacek, a top AFL-CIO representative in Latin America, denied that U.S. labor worked with the FTV to plan the Venezuelan coup. "We&#133;condemn any and all coups and unilateral seizures of power which destroy and undermine democratic institutions, including in Venezuela," he wrote in a statement distributed over the Internet.</P>
<P>But Kim Scipes--a longtime labor activist who has written widely on the efforts of U.S. unions to suppress democratic labor movements in other countries--says that Gacek's claims aren't convincing. "For the sake of the well-being of the U.S. labor movement and labor around the world, the AFL-CIO must cut all its ties with the National Endowment for Democracy and&#133;'come clean' on their past and present labor operations," she wrote on the ZNet Web site.</P>
<P>In fact, in the run-up to last year's AFL-CIO convention, activists in West Coast labor councils used Scipes' work to pass resolutions calling on the federation to open its files. The effort to expose labor's foreign policy secrets can be part of an effort to open a genuine debate in the labor movement over international issues.</P>
<P>For example, besides supporting Bush's "war on terror," virtually every top union leader lines up behind Israel, despite its one-sided war on Palestinians. For his part, Sweeney was a speaker at the April 15 "Rally for Israel" alongside right-wingers.</P>
<P>Efforts to challenge the pro-war, pro-imperialist positions of labor leaders have so far been modest, but are still important. Groups like New York Labor Against War and Labor for Peace and Justice in San Francisco have given some local union leaders and activists a platform to oppose Bush's war and his attack on civil liberties.</P>
<P>With the social costs of increased military spending mounting--and the terrible casualties that would come from Bush's planned invasion of Iraq--there will be more opportunities to argue that workers have no interest in supporting Washington's coups and wars.</P>

