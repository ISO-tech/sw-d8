<P><font face="Times New Roman, Times, serif" size="5"><b>Good riddance to <I>Ally McBeal</I></b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 17, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MAYBE THERE is a god.</P>
<P>Five years ago, the Fox network introduced <I>Ally McBeal</I>--a show focused on the wacky antics of the self-absorbed Harvard Law graduate and her coworkers at a high-powered Boston law firm.</P>
<P>TV audiences were subjected to watching McBeal's imagination run wild--from the dancing baby who represents her "ticking biological time clock" to the snarling cat sounds in her head when she sees a woman who she considers a threat.</P>
<P>The coworkers are equally insipid--from the ex-boyfriend who joins the men's movement to a selection of "man-eating" women.</P>
<P>A year after the show began airing, <I>Time</I> magazine featured Calista Flockhart, the show's star, on its front cover, flanked by Gloria Steinem and Susan B. Anthony. The words below asked, "Is feminism dead?"</P>
<I><P>Time</I> used <I>Ally McBeal</I> as its symbol for "a post-feminist era"--meaning that the activism of the women's movement of the 1960s and 1970s was officially pass&eacute;. The demands of that movement--equal pay for equal work, access to abortion and affordable child care--were equally as dated, according to <I>Time</I>.</P>
<P>Of course, <I>Ally McBeal</I> didn't do all this. Throughout the 1990s, the right wing had made a project of attacking anything it called "politically correct." Women who made charges of sexism became "whiners" who were restricting the "freedom of speech" of others.</P>
<P>Some self-proclaimed feminists didn't exactly fight this attack. Middle-class "power feminists"--such as Katie Roiphe, who made her career trying to disprove the existence of date rape--focused on the importance of women making it in "a man's world" without depending on "special rights."</P>
<P>The upshot is that we're left today with complete confusion over not only how to fight sexism, but whether it even exists. <I>Ally McBeal</I> certainly didn't single-handedly make this so--but it was a sickening reflection of it.</P>
<P>As in the episode in which the head of the law firm argues in court that "disgruntled lesbians are the driving force" behind sexual harassment laws, <I>Ally McBeal</I> regularly made sexual harassment the butt of its jokes.</P>
<P>Other lawsuits on the show included a member of the firm suing men for having sexual thoughts about her--and a woman charging date rape after she learned that the man had done advance research on her hopes and dreams.</P>
<P>Pretty funny stuff--if you think that women who make sexual harassment or rape complaints are "just overacting." I guess it's pretty funny, too, if you're a lawyer with a six-figure salary who doesn't relate to most working women--you know, the ones who make low wages and go without health insurance.</P>
<P>Disgustingly, the closest that the mainstream media came to criticizing <I>McBeal</I> were the cruel jokes about Flockhart's dangerously anorexic appearance--a body type that the advertising industry uses daily to sell any number of products, and yet escapes criticism.</P>
<P>Fox said <I>McBeal </I>was being canceled because of its flagging ratings--even after a campaign to prop it up with celebrity and rock star guests. But it's safe to assume that the network won't be replacing the show with anything that depicts the lives of real women--or men for that matter.</P>
<P>Still, it's nice to see her go. So goodbye, Ally McBeal. Don't let the door hit you on the way out.</P>

