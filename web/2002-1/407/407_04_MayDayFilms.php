<P><font face="Times New Roman, Times, serif" size="5"><b>More May Day suggestions</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 17, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>Your recent review of books and movies to celebrate May Day (<I>SW,</I> May 3) left out a few excellent films that I feel offer rich and inspiring views of workers and oppressed people in struggle. Two films by Ken Loach come to mind: <I>Land and Freedom</I> and <I>Bread and Roses</I>. </P>
<I><P>Land and Freedom</I> is a beautifully made political film about the Spanish Civil War. <I>Bread and Roses</I> follows the travails of an immigrant woman as she struggles for dignity by organizing a union in her workplace. It's based on the Justice for Janitors campaign in Los Angeles.</P>
<P>Another good movie is the made-for-TV film <I>Harlan County War, </I>which stars Holly Hunter. <I>Harlan County War</I> dramatizes the fight to organize a coal mine in Kentucky.</P>
<B><P>John Kirkland, </B>Trenton, N.J.</P>

