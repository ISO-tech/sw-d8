<P><font face="Times New Roman, Times, serif" size="5"><b>Grand theft energy in California</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 15, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>ALAN MAASS looks at Enron's life of crime.</P>
</B><P>LAST APRIL, former Enron CEO Ken Lay dropped in on an old friend who had just landed a new job in Washington, D.C. The two had a productive conversation<I>--</I>thanks to an eight-point memo that Lay had thoughtfully prepared.</P>
<P>The memo is filled with business jargon about "methods of determining the cost-base of wholesale power" and so on. But really, Lay had a very simple message for his friend: Don't let the federal government put limits on how much Enron and other companies can charge for electricity.</P>
<P>And speaking for the Bush administration, Vice President Dick Cheney responded: Don't worry, Kenny Boy, we've got you covered.</P>
<P>At the time, Enron and a handful of giant power companies were carrying out one of the most brazen corporate crimes in history<I>--</I>the great California electricity robbery. Because of a deregulation law<I>--</I>passed unanimously by the California legislature in 1996<I>--</I>utility companies that provided electricity to state consumers were required to buy wholesale supplies on the free market, from companies like Enron.</P>
<P>Deregulation was supposed to lead to lower electricity bills<I>--</I>because competing companies would offer lower prices to provide power to the state. Predictably, the opposite happened<I>--</I>with nothing to stop them, the power giants worked together to jack up prices.</P>
<P>In 1999, California paid a total of $7.5 billion on electricity supplies. One year later, it was paying $28 billion. No wonder Lay wanted to block federal price limits.</P>
<P>But if California was their best-known heist, the Enron gang already had a long life of crime behind them. The company's specialty was theft<I>--</I>or more accurately, exploiting the politicians' craze for deregulating the power industry.</P>
<P>Enron's method was to swoop in after government-regulated power systems were opened up to private companies<I>--</I>and "make money in the initial chaos," as one Enron executive told the <I>New York Times</I>.</P>
<P>After starting out as an operator of natural gas pipelines, by the 1990s, Enron had less and less to do with any part of the process of actually producing power. Instead, it became a corporate middleman<I>--</I>buying supplies from natural gas producers at the lowest possible price, then selling for as much as possible to power plant operators; buying electricity from power plants and selling to utilities to transmit to consumers.</P>
<P>The mega-profits came from gambling on the prices that power was bought and sold for<I>--</I>the same way that Wall Street speculators make money gambling on stocks. Enron's trades grew incredibly complex, with the company agreeing to buy or sell power at a predetermined price far into the future<I>--</I>sometimes as many as two decades ahead. Trying to guess a price that far in advance is no more accurate than trying to hit the lottery.</P>
<P>Enron's secret of success was that there were so few other buyers and sellers<I>--</I>only so many companies produced natural gas, only so many needed to buy it to run power plants, and fewer still had the massive sums to be the middleman. In spite of all its rhetoric about free-market competition, Enron succeeded by dominating the market<I>--</I>gaining monopoly power to effectively set prices.</P>
<P>The result? On the day after Enron declared bankruptcy, prices on new "forward contracts" for energy<I>--</I>that is, contracts to buy or sell energy at a set future point<I>--</I>dropped by 30 percent. In other words, even as its business was collapsing, Enron was powerful enough to inflate the prices that its competitors, even the biggest, paid for energy.</P>
<P>And who got stuck with the bill for this gouging? Ordinary people, of course. In California, average electricity bills jumped by 40 percent, and state officials dipped into tax revenues to spend $11.3 billion on massively overpriced power. Plus the state is committed to long-term contracts to buy energy that are inflated by an estimated $22 billion<I>--</I>so the rip-off will keep paying off for years to come.</P>
<P>And all of it was legal. Washington's investigation into Enron's collapse will focus on the company's financial shenanigans<I>--</I>hiding debts run up when the company tried to duplicate its success at energy trading by expanding into everything from paper to Internet bandwidth to the weather.</P>
<P>But we shouldn't let this obscure the crimes that Enron committed that were perfectly legal.</P>

