<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Behind Bush's talk of an "evil axis"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 15, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AS GEORGE W. Bush was gearing the country up for war last fall, he and his media acolytes talked about seeking "justice" for the victims of the September 11 attacks.</P>
<I><P>Socialist Worker</I> wrote that justice was the last thing on Bush's mind. Instead, we contended, Bush was wrapping himself in the tragedy of September 11 to advance U.S. geopolitical interests.</P>
<P>Those who believe that Bush's war on terrorism has anything to with justice should review the president's January 29 State of the Union address. In one of the most war-mongering speeches a U.S. president has ever given, Bush told the world that it had better get in line or face the U.S. war machine.</P>
<P>Bush didn't mention Osama bin Laden, who he had wanted "dead or alive" only a few months before. He barely mentioned the al-Qaeda network. Instead, he conjured up an "axis of evil," composed of Iraq, Iran and North Korea, that the Pentagon had to be prepared to fight.</P>
<P>The idea that these countries could pose a threat to the U.S. is ludicrous. The U.S. spends more on its military than the gross national products of these countries combined. But this isn't the first time U.S. policy makers have turned these three countries into a rationale for further Pentagon waste.</P>
<P>A decade ago, after the USSR collapsed, Pentagon chiefs had to figure out a way to justify maintaining a military budget at near-Cold War levels after the rationale for a Cold War-sized military had disappeared. The answer was to propose that the U.S. military be prepared to fight two simultaneous "major theater wars" on either side of the world against "rogue nations," namely, Iraq, Iran and North Korea.</P>
<P>Months before September 11, the Pentagon abandoned the "two war" doctrine, making clear, however, that the U.S. "is not abandoning planning for two conflicts to plan for fewer than two&#133;The [Department of Defense] is changing the concept altogether by planning for victory across the spectrum of possible conflict."</P>
<P>So Bush's "axis of evil" and "war on terrorism" rhetoric merely supplies the latest high-flown justification for the military's plan for permanent war<I>--</I>a plan in the works long before September 11.</P>
<P>Almost from the day it arrived in office, the Bush administration has schemed for ways to attack Iraq. In his January 29 speech, Bush began to build a case for just such an attack.</P>
<P>Knowing that Americans don't like the idea of going to war to defend oil profits, Bush put the case in the standard "good vs. evil" framework: "This is a regime that has already used poison gas to murder thousands of its own citizens<I>--</I>leaving the bodies of mothers huddled over their dead children."</P>
<P>Bush was correct. The Iraqi government killed more than 5,000 Kurds in a poison gas attack in 1988. Only the U.S. didn't really protest at the time. Saddam Hussein was then Washington's ally against another part of the "axis of evil," Iran.</P>
<P>In fact, officials of the Reagan and Bush Sr. administrations did their best to cover up the slaughter so they could get back to the business of selling Saddam components to build weapons of mass destruction.</P>
<P>Four months before Iraq invaded Kuwait in 1990, a delegation of U.S. senators, acting with personal authorization of then-President George Bush, met with Saddam to assure him of U.S. support.</P>
<P>In 1992, congressional investigators were closing in on Bush Sr.'s "Saddamgate." Bush's failure to win re-election rendered these investigations moot. But there is still plenty of evidence that makes a mockery of the Bush gang's claim to be fighting "evil" in Iraq.</P>
<P>Perhaps that's one reason why Dubya wants to make it next to impossible for the public to obtain records from the Bush Sr. and Reagan administrations. As Dubya might say, "This is a regime that has something to hide from the civilized world."</P>

