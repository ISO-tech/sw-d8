<P><font face="Times New Roman, Times, serif" size="5"><b>We can't wait for Democrats to fight</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 15, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>I think Douglas Murray's letter, "You can't ever trust Democrats" (<I>SW,</I> February 8) overlooks a key point in its criticism. It would be a good thing if the Democrats actually did fight back against Bush and the Republicans.</P>
<P>If the Democrats stood up to Bush, it would help to counter the current right-wing onslaught<I>--</I>and be a shot in the arm to working people's confidence to fight back.</P>
<P>Imagine if the large liberal groups that traditionally tail the Democratic Party began to take a stand. What if the National Organization for Women put real muscle into taking on Bush's anti-abortion lunacy? What if the AFL-CIO had real campaigns to fight layoffs?</P>
<P>We haven't seen mobilizations like this in a while. But no one should underestimate the ability of Democrats to try to put themselves at the head of a movement.</P>
<P>This isn't an argument to have illusions in the Democrats. In fact, within these movements it's an absolute necessity to always raise why we don't think the Democrats are the "friends of working people."</P>
<P>What's frustrating now is that a year into Bush's administration, the Democrats have remained spineless in the face of every crazy right-wing policy. That's why we don't wait for the Democrats to organize<I>--</I>we build the fightback here and now.</P>
<B><P>Elizabeth Lalasz,</B> Chicago</P>

