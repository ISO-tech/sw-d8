<P><font face="Times New Roman, Times, serif" size="4">Botched U.S. raid on an Afghan school</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Massacred by Special Forces</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 15, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"THIS HAS been the most accurate war ever fought in this nation's history." So Gen. Tommy Franks told the Senate Armed Services Committee last week. But Niaz Mohammed has a different opinion about the "accuracy" of U.S. forces.</P>
<P>On January 23, Mohammed was sleeping, along with dozens of other men, in a religious school in the village of Khas Uruzgan in southern Afghanistan. At 3 a.m., U.S. Special Forces soldiers burst in and started shooting.</P>
<P>The U.S. military claimed that the school was a Taliban weapons hideout. In reality, it was being used to store ammunition and weapons seized in an arms-collection program organized by the new government that replaced the Taliban.</P>
<P>But U.S. commandos opened fire first<I>--</I>and never bothered to ask questions. In all, 21 people were killed, and 27 captured and imprisoned for more than a week.</P>
<P>"We were all sleeping," Mohammed told the Associated Press. "They didn't give us a chance to surrender. They came to kill us." He escaped by jumping out an open window.</P>
<P>Another man named Amanullah hid overnight in a nearby mosque. He told reporters that he watched as his cousin struggled with U.S. soldiers. In the morning, he found his cousin's body, riddled with bullets to his neck, shoulders and back<I>--</I>all fired from behind. His cousin's hands were tied behind his back with white plastic handcuffs<I>--</I>labeled with the words "Made in the USA."</P>
<P>Meanwhile, the 27 men taken into custody and held at a U.S. military camp were subjected to severe beating and degrading conditions. Prisoners said that they were chained together on the floor as U.S. soldiers walked on their backs.</P>
<P>One of the victims, 60-year-old Abdul Rauf, was beaten until he suffered cracked ribs and bruised kidneys. He still can't stand because of the beating he received. "I can never forgive them," Rauf told reporters. "Why did they bomb us? Why did they do this?"</P>
<P>After a week of this, the Pentagon was forced to free the detainees and admit that they weren't Taliban or al-Qaeda members. But don't expect an apology. "The release of the detainees isn't an admission that we made a mistake," sneered Maj. Ralph Mills.</P>
<P>Even so, the U.S. government is apparently trying to "make amends." Last week, according to news reports, CIA agents went to Khas Uruzgan to pay each of the families whose loved ones were murdered approximately $1,000.</P>
<P>Apparently, the U.S. government thinks that this is the going price for the life of an innocent Afghan civilian.</P>

