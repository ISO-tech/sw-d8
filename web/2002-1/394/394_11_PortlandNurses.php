<P><font face="Times New Roman, Times, serif" size="4">Portland nurses end their two-month strike</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"I gained a sense of pride"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul Dean</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 15, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>PORTLAND, Ore.<I>--</I>Nurses at Oregon Health Science University (OHSU) Hospital voted 736 to 118 to return to work after a strike of almost two months.</P>
<P>OHSU spent enormous amounts of money to defeat the strike by the Oregon Nurses Association (ONA), bringing in scab nurses from California.</P>
<P>Other Portland unions and nurses from around the state supported ONA's Saturday unity rally to celebrate the nurses' strike. Chanting "Nurses united will never be defeated," and "We are the nurses, the mighty, mighty nurses," they marched past OHSU to let the scabs know that striking nurses have power.</P>
<P>The new contract will give the nurses 7 percent raises the first two years and a 6.5 percent raise in the third year. And OHSU agreed to develop a health care plan that would be more affordable to nurses by 2004.</P>
<P>"We didn't get everything we wanted, by any means," said nurse Ray Hogan. "However, the gains we made should help recruit good nurses and stop the hemorrhaging of the nurses we've got."</P>
<P>Across the board, nurses felt the strike was worth it because it increased their leverage with respect to management. "I gained an amazing sense of pride and a sense of power," said nurse Dana Welty. "I'm a different person. And I feel I've done more for my profession in the last two months than I've done in my entire 15-year career."</P>
<P>Mike Brian, a nurse from Pendleton, Ore., told the rally that the strike would affect every nurse in the state and get national recognition. Nurses from nearby St. Vincent's Hospital said the OHSU strikers had given them confidence and inspiration for their upcoming contract.</P>
<P>"Things will be different at OHSU from now on," said ONA labor negotiator Kathleen Sheridan after the vote count. "These nurses took a stand for their profession and for their patients."</P>
<P>"Why had OHSU prolonged the strike?" she asked. "The nurses involved in this strike can only come to the reasonable conclusion that management's actions during negotiations have been all about maintaining a posture of control and an unwillingness to share it. When the nursing care of patients<I>--</I>and the welfare of those who provide it<I>--</I>becomes a permanent focus of our employers, then<I>--</I>and only then<I>--</I>will we rest."</P>
<P>"The monetary settlement is okay," Stew, a striking nurse, told <I>Socialist Worker</I>. "But some of the issues that are on paper is going to force them to be more accountable, and we're going to be more involved in decision making."</P>
<P>After the rally, some of the nurses went to support a picket at a Fred Meyer supermarket where janitors organized by Service Employees International Union (SEIU) Local 49 are fighting for a living wage and health care coverage. SEIU Local 49 workers had been active supporters of the nurses throughout the strike.</P>
<P>This strike has shown the true spirit of solidarity in Portland, which will last for a long time.</P>

