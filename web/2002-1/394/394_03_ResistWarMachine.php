<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Washington gets ready to take its war on the road</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Building resistance to the war machine</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 15, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>MIR AHMAD and his two friends were searching a hillside in eastern Afghanistan last week for scrap metal to sell. That's when the CIA obliterated them with a Hellfire missile. "The three were cut in half," villager Zawar Khan told the <I>Washington Post</I>. "They were just poor people trying to get money to feed their families."</P>
<P>Why were they targets? Ahmad had the misfortune to be a little taller than average<I>--</I>which may have led CIA agents to think that he was Osama bin Laden.</P>
<P>But don't think U.S. officials will be admitting that a mistake was made. At the Pentagon, when innocent people are killed by U.S. soldiers or warplanes, it's an "unfortunate consequence of war."</P>
<P>Then again, when it comes to pointing fingers and accusing other countries of being terrorists, there's nobody more enthusiastic than the Bush gang. The administration is getting ready to take its war on the road, and topping the hit list are Iraq, Iran and North Korea<I>--</I>labeled an "axis of evil" by George W. Bush in his State of the Union address.</P>
<P>Exactly what connects them is anybody's guess. Iraq and Iran fought a bloody war in the 1980s, and famine-plagued North Korea is no threat to anyone. But the Bush gang cares more about beating the war drums than the facts. They'll go to any length to whip up a war frenzy.</P>
<P>In one of the sleaziest examples yet, the White House launched a new $10 million ad campaign<I>--</I>complete with top-dollar TV time during the Super Bowl<I>--</I>that links terrorism to drug use in the U.S. The ads show drug-addicted teenagers saying, "I helped murder families in Colombia"<I>--</I>supposedly because profits from drugs end up in the hands of terrorist groups.</P>
<P>In fact, Americans do unwillingly finance terrorism in Colombia<I>--</I>because their tax dollars are dished out to the Colombian government to fuel its four-decade-old dirty war against left-wing rebels.</P>
<P>Just like they finance the brutal warlords of Afghanistan's Northern Alliance. According to the <I>Washington Times,</I> the Bush administration last year funneled $200,000 each to more than 35 warlords to win their cooperation in the war against the Taliban.</P>
<P>And the Bush administration dares to claim that it went to war for justice! The reality is clearer than ever today<I>--</I>that Washington's war makers care only about proving that the U.S. war machine will do whatever it wants, wherever it wants.</P>
<P>It's also clear that the thousands of people who organized against Bush's war were right. The "victory" in Afghanistan last year may have disoriented this opposition, but the potential for rebuilding resistance is growing again<I>--</I>as antiwar activists learned at the New York City demonstrations against the World Economic Forum, where their slogans were widely taken up. We need to build on this.</P>
<P>On February 14, relatives of victims of the World Trade Center attack will launch September 11th Families for Peaceful Tomorrows, a group to oppose Bush's war drive. Student activists from across the country will gather February 22 at Columbia University in New York for a national student antiwar conference. And activists have begun planning for a week of demonstrations in Washington, D.C., in late April to target the war and other issues.</P>
<P>These events can re-energize the opposition to Bush's war<I>--</I>and lay the groundwork for a larger and stronger movement.</P>

