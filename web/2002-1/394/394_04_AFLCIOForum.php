<P><font face="Times New Roman, Times, serif" size="4">Missing from the AFL-CIO forum in New York</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Why weren't rank and filers invited?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 15, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>As part of the opposition to the World Economic Forum in New York, the AFL-CIO hosted an excellent roundtable discussion on January 31. Titled "Voices the World Economic Forum Will Not Hear," the event featured workers from Mexico, China, Guatemala and the U.S. who are the victims of neoliberal economic policies that WEF attendees are so fond of.</P>
<P>Sweatshop workers Sofia Sazo from Guatemala and Agnes Wong from New York City led off the event with stories about working 12- to 15-hour days making shirts for the Gap and Polo.</P>
<P>Russell Sheffler, a laid-off steelworker from LTV Steel in Cleveland, spoke of the hardships facing him since the company went bankrupt and took much of his retirement savings with it down the tubes.</P>
<P>Every story was both sad and inspiring, as one after another, these workers spoke of how they were fighting back. Workers and the WEF "are looking in two different directions," said Rogelio Sala, a former bartender from New York City who was fired for union organizing. "We don't want the same things, and it is time for us to take action." The audience enthusiastically agreed.</P>
<P>But missing from the audience was one key element<I>--</I>rank-and-file workers. The event was held at 2:30 p.m. on a Thursday, when most workers are still on the clock. Reservations were required for the small auditorium, and even though I called two weeks in advance, I was denied a ticket because I wasn't a union staffer or official.</P>
<P>By pure luck, a member of the International Socialist Organization who was working in the building let me watch the discussion from the technicians' booth.</P>
<P>As a steward, I can use the stories that these workers had to tell to inspire and enrage coworkers who often don't see why unions matter or how we are connected to an international labor movement.</P>
<P>The AFL desperately needs an active membership with a clear idea of how globalization works and how union workers in America are better positioned than anyone else to lead this fight.</P>
<P>The entire event was a clear reminder that this fight can only be led by rank-and-file workers themselves.</P>
<B><P>A member, </B>CWA Local 1109, New York City</P>

