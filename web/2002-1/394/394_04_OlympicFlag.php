<P><font face="Times New Roman, Times, serif" size="5"><b>Cynical flag stunt at Olympics</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 15, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Recently, the International Olympic Committee decided to allow the American team to carry a tattered flag recovered from the rubble of the World Trade Center.</P>
<P>The U.S.<I>--</I>the world's biggest bully, which has stepped on so many countries and killed so many innocent people<I>--</I>now gets to play the victim? This is ridiculous!</P>
<P>What if Iraq decided to fly its tattered flag because its people have been starved and bombed for almost by a decade by the U.S. and Britain? There would be an outcry of scorn, anger and indignation from the "international community"<I>--</I>with the U.S. in the front ranks.</P>
<P>Today, everything is done in the name of the victims of September 11<I>--</I>from bombing Afghanistan to abusing prisoners at Camp X-Ray in Guant&aacute;namo Bay, Cuba. This cheapens the enormous loss and grief of the victims' families.</P>
<P>Don't be fooled by rhetoric about victims, and don't stand behind the tattered flag. The U.S. government doesn't give a damn about victims<I>--</I>abroad or at home.</P>
<B><P>Pham Binh,</B> Geneseo, N.Y.</P>

