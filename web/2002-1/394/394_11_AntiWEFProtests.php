<P><font face="Times New Roman, Times, serif" size="5"><b>Fighting for global justice</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Annie Levin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 15, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P><STRONG>ACTIVISTS IN several cities protested in solidarity with demonstrations against the World Economic Forum (WEF) meetings in New York the weekend of February 2.</STRONG>
</P>
<P>--In Boston, 500 workers representing 40 unions and community groups from across Massachusetts came to a speak-out on the effects of the recession.</P>
<P>At the event, chaired by veteran civil rights activist Mel King, a state AFL-CIO representative said that Massachusetts is the only state where the median income declined during the 1990s. Now that layoffs have hit, the current level of funding for job training will only cover one in 21 laid-off workers.</P>
<P>A 30-year veteran of Polaroid described how his entire retirement fund was wiped out when the company went bankrupt last year. "I lost everything&#133;It's time to poke holes in those golden parachutes," he said.</P>
<P>Gary Nillson, secretary of Communications Workers of America Local 1365, which represents workers at Lucent Technologies, spoke bitterly about the North Andover plant that once employed 10,000. Since the latest round of layoffs, about 500 jobs remain.</P>
<P>Lucent "came into our community, made billions off the backs of workers, exploited our air and our water, and now they are willing to destroy the lives of thousands and leave workers devoid of their dreams, desires and aspirations."</P>
<P>"For welfare recipients, there was no boom," said Ken Ramsey, president of Service Employees International Union Local 509, which represents social workers. "We've been working in a downturn for 24 years. A country with this wealth should not have one person go hungry, not one person go without health care or a job. Not when this country continues to produce and sell more arms than any other country on earth."</P>
<P>Many spoke about the connection between the budget cuts and the war. "Don't let Bush take our anger and focus it on people in some other country when the real terrorists are here at home," said Boston City Councilor Chuck Turner.</P>
<P>--In Seattle, 100 people marched against the WEF, chanting "Hey George Bush, it's not a pretty picture, the poor get poorer and the rich get richer!" and "WEF, WTO, Seattle people still say 'No!'" They marched to Westlake Mall, the site of confrontations with police during the 1999 "Battle of Seattle."</P>
<P>--In San Francisco, 60 people attended a panel about the priorities of the giants of global capitalism and addressed local fightbacks in the Bay Area.</P>

