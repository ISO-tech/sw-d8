<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>How can racism ever be overcome?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 15, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH showed his contempt for the U.S. Commission on Civil Rights by trying to push through the appointment of conservative lawyer Peter Kirsanow. Kirsanow, a member of the right-wing Center for New Black Leadership, is known for his staunch opposition to affirmative action, which he calls not only a "colossal failure" but "racist."</P>
<P>Last month, Kirsanow "celebrated" Martin Luther King Day by addressing the conservative Heritage Foundation<I>--</I>to argue that African Americans needed to increase "personal responsibility" and stronger families. Rather than affirmative action, what's really needed from government is "color blindness."</P>
<P>When people like Kirsanow use phrases like "color blindness," they're trying to make two points. One is to claim that racism no longer exists<I>--</I>that U.S. society has developed to the point where discrimination is a thing of the past. The second is that minorities don't need programs, such as affirmative action, that try to put Blacks and whites on an even playing field.</P>
<P>A handful of highly successful African Americans<I>--</I>like Secretary of State Colin Powell, for example<I>--</I>are pointed to as proof that Blacks have every opportunity to succeed. But these special examples fly in the face of the reality experienced by most African Americans.</P>
<P>A 1999 study by the Impact Fund followed Black and white job applicants with similar experience and interview skills as they applied for a range of jobs in California's Bay Area. The study showed that employers chose the white over the African American applicant about 76 percent of the time.</P>
<P>And while the recession has meant higher unemployment for all workers, joblessness among Black workers was more than 10 percent in December.</P>
<P>These statistics show that racism clearly isn't in the past, but is part and parcel of the world today.</P>
<P>The flip side of the argument that racism is long past is the belief that racism can never be overcome. Racism, we're told, is part of human nature<I>--</I>so how can we ever expect whites to oppose racism on any significant scale?</P>
<P>The problem with this proposition is that it removes racism from its material roots. Just as a racist ideology that dehumanized Blacks was necessary to prop up the barbaric system of slavery in the past, our rulers use racism today.</P>
<P>The gap between Blacks and whites in wages, housing and education<I>--</I>along with the fostering of racist ideas alongside this<I>--</I>isn't an accident. These factors play a crucial role in maintaining the status quo.</P>
<P>As an employer in the Jim Crow South explained it just over a century ago: "The white journeyman bricklayer gets $2.50 a day, and we are able to employ a colored bricklayer for $1.75. If the white bricklayer asks for employment, the employer may say to him, I can employ a Negro bricklayer who has as much skill as you, and will do as good service for $1.75. Now, I will put you on at $2.25."</P>
<P>The great abolitionist leader Frederick Douglass put it even more simply: "They divided both to conquer each."</P>
<P>Many people today<I>--</I>including many whites among them<I>--</I>are appalled by racism and would like to see an end to it. This moral outrage is powerful in its own right.</P>
<P>But underlying it is a material reality: The racism that divides Black and white makes <I>all</I> workers weaker. Therefore, it's in the interests of all the oppressed to unite against those who control the system.</P>
<P>In fact, the history of U.S. workers' struggles is filled with stories of multiracial unity. That unity doesn't happen automatically. It has to be <I>built</I>. Those committed to the fight for a better world have to <I>organize</I> unity<I>--</I>on the basis of uncompromising opposition to racism and oppression.</P>
<P>But in the process of fighting together, people see through the lies that are told to them. They discover that racism isn't something you're born with, but is fostered from above.</P>
<P>This is the hope for abolishing racism<I>--</I>that the opposition to oppression among those affected becomes the cause of all.</P>

