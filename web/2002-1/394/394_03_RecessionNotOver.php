<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The recession that they want to hide</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 15, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"WHAT RECESSION?" asked <I>Business Week</I> magazine last week. Reports that the U.S. economy was rebounding<I>--</I>including a January Labor Department report of a decline in unemployment for the first time in seven months<I>--</I>set off celebrations on Wall Street.</P>
<P>But a look at the details reveals a different story<I>--</I>that "workers [are] falling out of the system," Wall Street analyst Ethan Harris said. "It's a sign of workers giving up."</P>
<P>According to Harris, some 924,000 people stopped actively looking for work<I>--</I>and therefore weren't included in the unemployment numbers. "The talk about the recession being over is probably wrong," he concluded.</P>
<P>You'd think that Washington would have acted by now to help working people hit hard by the recession. Think again.</P>
<P>Instead, George W. Bush and congressional Republicans tried to push through an "economic stimulus" package that would dole out enormous tax breaks to the biggest corporations. And while Democrats said they opposed these giveaways to the rich, their plan was stuffed with corporate goodies, too.</P>
<P>When all was said and done, all Senate Democrats delivered was a 13-week extension of unemployment benefits. This is a bit of good news for the 300,000 people whose jobless benefits ran out in December<I>--</I>and the 2 million who will lose them in the coming six months. But it's nowhere near what's needed to ease the misery that the recession will bring.</P>
<P>For one thing, it does nothing for the 62 percent of workers who aren't even eligible for jobless benefits. And while Democrats bicker with Republicans over meager measures for working people, they haven't made a peep about Bush's proposal to pump $48 billion more down the Pentagon rat hole.</P>
<P>But don't expect the Democrats to point this out<I>--</I>since they serve the same corporate masters as Republicans. That's why people around the country are looking for ways to organize<I>--</I>in local struggles that often go unreported in the media.</P>
<P>Revitalizing our unions, taking up the struggle for our schools, fighting for affordable housing<I>--</I>this is where real resistance can be built.</P>

