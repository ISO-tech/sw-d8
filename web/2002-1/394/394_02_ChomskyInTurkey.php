<P><font face="Times New Roman, Times, serif" size="5"><b>Turkish regime puts free speech on trial</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 15, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>VETERAN POLITICAL activist and author Noam Chomsky will travel to Turkey this week for the trial of a publisher facing prison for daring to publish books critical of the country's regime.</P>
<P>Fatih Tas of Aram Publishing House<I>--</I>the Istanbul-based publisher of Chomsky's book <I>American Interventionism--</I>is accused of publishing "propaganda against the indivisible unity of [the] country, nation and state." If found guilty, Fatih could be sentenced to a year in prison.</P>
<P>The accusations revolve around Chomsky's documentation of the Turkish government's bloody war against minority Kurds. "The Kurds have been miserably oppressed throughout the whole history of the modern Turkish state," Chomsky writes in one of the essays in <I>American Interventionism</I>.</P>
<P>"In 1984, the Turkish government launched a major war in the southeast against the Kurdish population&#133;The end result was pretty awesome: tens of thousands of people killed, 2 to 3 million refugees, massive ethnic cleansing with some 3,500 villages destroyed."</P>
<P>Chomsky is traveling to Turkey to show his support for Fatih, but he could potentially be put on trial himself. In addition to attending the trial, Chomsky plans to try to meet with Kurdish independence activists<I>--</I>in a test of the government's claim to have passed new laws guaranteeing freedom of expression.</P>
<P>Meanwhile, the U.S. government and its allies in the "war on terrorism" have been silent about Turkey's war on the Kurds and its crackdown against all political opponents. That's because Washington cares more about maintaining ties to an important ally in a key region of the world.</P>
<P>Chomsky's show of support for Fatih is an important opportunity to expose the brutal crackdown on dissent in Turkey and by other U.S. allies.</P>
