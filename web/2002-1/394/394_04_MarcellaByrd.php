<P><font face="Times New Roman, Times, serif" size="5"><b>Her death was an execution</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 15, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>On January 19, Marcella Byrd left a supermarket in Long Beach, Calif., allegedly without paying. Shortly afterward, she was shot to death by the Long Beach police.</P>
<P>Police reported that Byrd had a steak knife that she refused to drop as she headed toward the main street downtown.</P>
<P>She was a 57-year-old mother who lived in a senior citizens' complex. She was diagnosed 20 years ago with schizophrenia. "We think that this was nothing less than a summary execution," lawyers for her family told the <I>Los Angeles Times</I>.</P>
<P>The officers who killed her described Byrd as an "out-of-control behemoth who knocked over a grocery store and tried to get away." This is in spite of the fact that she was just 4 feet 5 inches tall!</P>
<P>"I'm still in shock. I can't understand it," her son, Tirus Parhms, told reporters. "My mother was not a threat to anyone."</P>
<P>Byrd's family is suing, claiming that the city "negligently hired, trained, managed and/or supervised each of the Long Beach officers involved in this incident."</P>
<P>Any money that they receive will be little compensation for a mother who was tried and executed within minutes by police officers intent on protecting the grocery store she is accused of "robbing."</P>
<P>But hopefully, her family's action will help to bring about a call for change.</P>
<B><P>Sarah Knopp,</B> Los Angeles</P>

