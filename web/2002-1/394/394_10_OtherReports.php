<P><font face="Times New Roman, Times, serif" size="4"><b>Stop Maryland's death penalty</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mike Stark and John Mayer</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AFTER MORE than three years without an execution, the state of Maryland was prepared to execute Steven Oken in early March. To make matters worse, four other inmates have reached the end of their appeals processes, setting the stage for a possible total of five executions in 2002<I>--</I>more executions in one year than have occurred in Maryland since 1943.</P>
<P>But all of these preparations ground to a halt February 6 when the Maryland Court of Appeals voted 6-1 to grant Oken a stay until the U.S. Supreme Court can hear his appeal.</P>
<P>Maryland has the highest percentage of Blacks on death row of any state. Of the five men facing executions this year, four are Black, and all five are accused of killing whites.</P>
<P>Now it's likely that no more death warrants will be signed in Maryland until the fall. But this is no time to relax<I>--</I>now more than ever we must be willing to go on the offensive against this racist system.</P>
<P>Polls continue to show that about half of Maryland residents support a moratorium, with even higher support among Blacks<I>--</I>about 65 percent. We must use this time to expose the system for what it is and build a vocal movement willing to take to the streets to demand abolition.</P>
<I><P>For more information about a February 23 march on Maryland's death row, contact the Campaign to End the Death Penalty at 202-726-1151 or cedp_dc@hotmail.com.</P>
</I>
<P><font face="Times New Roman, Times, serif" size="4"><b>Defend immigrants' rights</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Afsaneh Moradian</b></font>
<font face="Times New Roman, Times, serif" size="3"><P>ELEVEN PROFESSORS and students at City University of New York (CUNY) schools held a three-day hunger strike last month to oppose a tuition hike imposed on undocumented immigrant students.</P>
<P>CUNY administrators decided last September to charge undocumented students out-of-state tuition rates<I>--</I>a huge increase. School officials say that the increase is to comply with a 1996 federal law, but CUNY ignored the law until after September 11.</P>
<P>The hunger strike to protest the tuition hike<I>--</I>as well as daily rallies at the end of business hours<I>--</I>drew international media attention to this issue. But a judge recently threw out a lawsuit to challenge the tuition hikes.</P>
<P>We need to keep demanding access to CUNY for everyone.</P>

