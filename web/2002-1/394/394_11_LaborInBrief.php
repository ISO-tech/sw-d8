<p><a name="Chojczak"></a><font face="Times New Roman, Times, serif" size="4"><b>Macy's</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Kevin Chojczak</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SAN FRANCISCO<I>--</I>More than 100 people gathered in front of the Union Square Macy's on February 2 to oppose the firing of Alia Atawneh.</P>
<P>On September 27, Alia, a Palestinian American woman, was working as a clerk at a Macy's in the nearby city of San Jose when a customer denounced "her people" and said she "had no right to work in the U.S." because of the September 11 attacks. When Alia asked what evidence the customer had that "her people" were responsible, he called the manager. A week later, Alia was fired.</P>
<P>The powerful display of solidarity by antiwar, union and community activists drowned out the droning Macy's shopping music. "I'm impressed with the people who are stopping by from the street to hear about Alia's case," Sarah, a community activist from the newly formed group called If Americans Knew, told <I>Socialist Worker</I>.</P>
<P>"The perpetual war Bush is waging is to perpetuate the war at home as millions are being cut from education, health care, and social services," said Steve Zeltzer of the Labor Video Project. "The attack on immigrants is part and parcel of this."</P>
<P>"It has been 'open season' on Arab immigrants and people of Middle Eastern background," said Osama Qasem, president of the San Francisco chapter of the American Arab Anti-Discrimination Committee.</P>
<P>Alia and her husband were the last speakers of the day. "Without this support we would not be able to express and stand up for our rights."</P>

