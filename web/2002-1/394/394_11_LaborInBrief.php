<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 15, 2002 | Page 11</font></p>

<p><font face="Times New Roman, Times, serif" size="3">OTHER STORIES BELOW<br>
