<p><a name="Flex"></a><font face="Times New Roman, Times, serif" size="4"><b>Flex-O-Tex</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Wengraf</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SOME 60 immigrant workers are striking for union recognition against the Flex-O-Tex laundry service in New York City. The strikers hope a union contract with UNITE will win them health care benefits and a pension.</P>
<P>Some employees who have worked at Flex-O-Tex for decades make little more than the minimum wage and receive no benefits at all. "The working conditions are terrible," Alberto Arroyo, UNITE's Amalgamated Service &amp; Allied Industries division secretary- treasurer, told <I>Socialist Worker</I>.</P>
<P>Workers held several rallies at the plant, and a contingent joined other UNITE locals at the 1,000-strong protest outside a Gap store in midtown Manhattan on January 31, during the week of protests against the World Economic Forum meeting of corporate fat cats.</P>
<P>"The union would give us what we need," a veteran worker of two decades told <I>Socialist Worker</I> at the Gap protest. "And we need to be out here to build solidarity with other workers."</P>
<P>Flex-O-Tex responded to the strike by using scabs<I>--</I>and says it is running at full capacity. But the company is facing troubles of its own<I>--</I>it defaulted on a loan from the city and could close its doors in the next two weeks.</P>
<P>The strikers at Flex-O-Tex deserve decent wages and benefits. They need support from other workers<I>--</I>especially in the city's almost all-union laundry industry<I>--</I>to win this battle.</P>

