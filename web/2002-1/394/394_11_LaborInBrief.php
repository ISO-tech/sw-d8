<P><font face="Times New Roman, Times, serif" size="4"><b>United Airlines</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MECHANICS AT United Airlines were in the midst of deciding whether to strike February 20 as <I>Socialist Worker</I> went to press.</P>
<P>Results of a membership vote on a contract offer recommended by the Presidential Emergency Board (PEB), appointed by George W. Bush, were due to be counted February 12.</P>
<P>After pleading poverty in negotiations, United reversed itself last month and accepted the PEB recommendation for a contract that would raise mechanics' wages by as much as 37 percent.</P>
<P>But it's an open secret that, however the mechanics' vote goes, management plans to demand a new round of concessions from all workers. So the choice facing mechanics<I>--</I>who haven't had a raise since 1994<I>--</I>is whether to fight now or later.</P>
<P>A vote to reject the contract would set the stage for a strike<I>--</I>putting the International Association of Machinists (IAM) on a collision course with George W. Bush, who has vowed to use his presidential powers to block any strike by airline workers.</P>
<P>Mechanics say that IAM officials have been tentative about taking a stand on the PEB proposal<I>--</I>but that rank and filers are fed up. "Two weeks ago, I was really worried people were going to accept the proposal," said Jennifer Biddle, a shop steward and member of the strike committee in IAM Local 1781 at United's San Francisco maintenance facility.</P>
<P>"People were saying the company might go bankrupt, and we could lose our jobs."</P>
<P>"But since then, people have spontaneously put out their own flyers in the absence of the union doing it, explaining why everyone should vote against the PEB proposal. They're all over the maintenance facility. I've not seen one flyer saying to vote for the PEB proposal. I think a lot of people feel that we were robbed once, and we're not going to be robbed twice."</P>

