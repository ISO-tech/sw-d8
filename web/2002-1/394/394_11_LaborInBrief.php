<p><a name="Harvard"></a><font face="Times New Roman, Times, serif" size="4"><b>Harvard University</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jessica Rothenberg</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CAMBRIDGE, Mass.<I>--</I>After three years of pressure, Harvard University President Lawrence Summers finally gave in to activists' demands and agreed to substantial raises for Harvard's janitors, gardeners, security guards and other low-wage workers.</P>
<P>Hundreds of Harvard workers currently make less than $10.25 an hour, and some are paid as little as $8.50 an hour. Summers agreed to raise base wages to between $10.83 and $11.30 an hour.</P>
<P>While the new pay rates still fail to establish a "living wage" of $15 an hour in order to support a family in Boston, they set a precedent for campuses around the country.</P>
<P>Last spring, the Living Wage Campaign occupied the first floor of Massachusetts Hall<I>--</I>which houses the University President's office<I>--</I>for three weeks. About 400 faculty members signed letters of support, and students also worked closely with Service Employees International Union Local 254<I>--</I>a great example of student-labor solidarity.</P>
<P>This victory should motivate activists on other campuses to launch similar campaigns and set a precedent for future successes.</P>

