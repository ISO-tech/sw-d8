<P><font face="Times New Roman, Times, serif" size="5"><b>Three-strikes law challenged in California</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sarah Knopp</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 15, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>EARNEST BRAY Jr. was charged with petty theft for stealing three videotapes in Los Angeles. And for that, he was thrown into a California prison for 25 years to life.</P>
<P>Bray is one of thousands of people sentenced under California's "three strikes and you're out" law<I>--</I>which requires a mandatory 25-years-to-life term if a defendant is convicted of a third felony, no matter how minor.</P>
<P>But after seven years of injustices since the law went into effect, a federal appeals court last week overturned Bray's sentence and hundreds of others, agreeing that a life sentence for shoplifting is cruel and unusual punishment.</P>
<P>The decision<I>--</I>the second in three months to overturn a three-strikes sentence<I>--</I>will open the way for hundreds of appeals from California prisoners in similar situations. Some 6,400 people have been sentenced under the three-strikes law, which was approved by voters in a ballot referendum in 1994.</P>
<P>The blatant injustice of putting people away for life for petty crimes has provoked criticism and anger. But state officials are ready to fight to keep the three-strikes law. The state attorney general will appeal last week's decision to the U.S. Supreme Court. And one lawmaker told the <I>Los Angeles Times</I>, "The court's decision today threatens to put more repeat murderers, robbers, rapists and child molesters back in our neighborhoods."</P>
<P>That disgusting rhetoric shows why the politicians wanted a three-strikes law in the first place. Harsh sentencing laws are a way of diverting attention from the social problems that they cause with their cutbacks<I>--</I>and onto scapegoats whose real "crime" is to live in poverty or have the wrong skin color.</P>

