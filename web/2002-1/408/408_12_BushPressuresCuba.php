<P><font face="Times New Roman, Times, serif" size="4">After Carter trip:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Bush ups pressure on Cuba</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 24, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH is seeing red. His administration is packed with right-wingers who were furious over former President Jimmy Carter's visit to Cuba last week. In a nationally broadcast speech, Carter denounced the Castro government as "undemocratic"--but also criticized the 40-year-old U.S. economic embargo of the island.</P>
<P>Carter justified his call for lifting the embargo with talk about helping Cuba's poor. But he also represents a wing of Corporate America whose interests aren't humanitarian.</P>
<P>Companies like food giant Archer Daniels Midland and drug maker Abbott Laboratories want access to Cuba's market--and the freedom to locate operations there to take advantage of the country's low-wage workforce, as their Canadian and European competitors increasingly have.</P>
<P>Bush, meanwhile, has to answer to the veteran Cold Warriors in his own administration--not to mention the fanatical Cuban exile community that helped him steal the White House in Florida. So Bush traveled to Miami earlier this week to promise that he wouldn't drop the embargo.</P>
<P>A few days earlier, he interrupted a photo op with Malaysian Prime Minister Mahathir Mohamad to declare to reporters that Castro "ought to have free elections," "ought to have a free press" and "ought to free his political prisoners."</P>
<P>Bush didn't seem the least bit embarrassed that he attacked Cuba while standing next to Mohamad--a dictator whose party has run Malaysia with an iron fist since 1957. Mohamad himself has been in office for two decades. He's held onto power by jailing his opponents under the infamous Internal Security Act.</P>
<P>"There are certain things that we can deal with by following the rules," explained Mohamad as Bush listened. "But at times we find the rules restrict you from doing the right things. On such occasions, we have to rethink--either you change the rules or break the rules."</P>
<P>What's "doing the right thing"? Mohamad came back with an example--detaining prisoners without trial!</P>
<P>But Mohamad gets the red-carpet treatment in Washington--while Castro is lectured about "free elections" by a U.S. president who lost the national vote. What hypocrites!</P>
<P>Bush could care less about a "free press" or "political prisoners"--as long as it's a U.S. ally carrying out the repression.</P>

