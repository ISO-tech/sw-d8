<P><font face="Times New Roman, Times, serif" size="4">Don't let them put Napoleon Beazley to death</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Stop the Texas killing machine!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lily Hughes</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 24, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>TEXAS IS once again earning its title as the death penalty capital of the world. The state plans to cap its latest killing spree--seven executions in a 30-day period--on May 28 with the murder of Napoleon Beazley.</P>
<P>Beazley came within four hours of being executed last summer when the Texas Court of Criminal Appeals agreed to hear his case. But the appeal was later denied. Now his last legal challenge lies with the U.S. Supreme Court. </P>
<P>Beazley's case has attracted international attention because it highlights so many of the injustices of the death penalty system. First of all, he was only 17 years old when he shot businessman John Luttig in a botched carjacking in 1995. But prosecutors sought the death penalty despite his age--and despite the fact that he had no criminal record, was his class president, and had just graduated 13th in his class.</P>
<P>If Beazley is put to death next week, Texas will add to its death toll of juvenile offenders--a practice that it shares with only a handful of countries in the world, including Pakistan, Congo, Iran and Nigeria.</P>
<P>Racism has also pervaded this case at every level. Beazley, who is Black, was tried by an all-white jury--though the population of Smith County is 20 percent African American. During the trial, a prosecutor called Napoleon a "lurking animal," and a post-conviction investigation revealed that one juror referred to Beazley as a "nigger" who "got what he deserved." </P>
<P>Then there's the race of the victim. John Luttig was white--which means that his assailant was many times more likely to get the death penalty. What's more, he was the father of Michael Luttig, a right-wing federal judge who pressured Texas prosecutors to seek a death sentence. When the U.S. Supreme Court heard Beazley's appeal earlier this month, three justices had to recuse themselves because of their relationships with the judge.</P>
<P>Beazley has been a model prisoner, and opponents of his execution say that no one who's met him can explain why he should die for a tragic and isolated mistake he made when he was 17. Even the prosecutor from Beazley's hometown has called for mercy.</P>
<P>Opponents of the death penalty are organizing forums and protests to cast a spotlight on this case--and how it exposes the injustices of the Texas execution machine.</P>
