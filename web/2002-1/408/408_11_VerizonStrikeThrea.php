<P><font face="Times New Roman, Times, serif" size="4">Union mobilizes for May 29 rally in New York City</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>CWA strike threat at Verizon</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By members of CWA Locals 1109 and 1106</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 24, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Verizon workers in New York State, members of the Communications Workers of America (CWA), are gearing up for a mass rally May 29 and a possible strike two days later.</P>
<P>Verizon has declared a "surplus" of 682 jobs and possibly 1,036 more, using an "external event" justification that allows the company to lay off workers once buyouts and elimination of subcontracting are completed. There have never been layoffs in the history of the phone company in New York.</P>
<P>The CWA is challenging Verizon's use of an "external event," which the contract states can only be invoked for a major event, like a loss of an entire line of business, but not an economic downturn.</P>
<P>In fact, while the company posted a loss of $500 million last quarter, this includes all foreign investments and business ventures. In the same quarter, Verizon gained almost 80 percent more customers in wireless and long distance. </P>
<P>If Verizon fails to arbitrate the "external event" declaration by May 31, the CWA can legally strike, citing an unfair labor practice by the company. </P>
<P>This follows months of insults and disregard for the contract since the union victory in a two-week strike in 2000. Despite agreeing to neutrality in that settlement, Verizon has refused to allow union elections in Verizon Wireless and has mounted legal challenges to stall unionization.</P>
<P>Recently, Verizon introduced a new speedup and discipline program called the Service Excellence Plan (SEP), which allows the company to suspend and eventually fire technicians who don't meet their production numbers.</P>
<P>The union leadership has been under increasing pressure from the rank and file to react to SEP. The company did suspend a brother in Local 1109 in the Central Avenue Brooklyn garage in early May, but rescinded it the same day for fear it wouldn't hold up under grievances. </P>
<P>Now, the added threat of repeated rounds of layoff--similar to the tens of thousands of jobs cut by AT&amp;T--has pushed the union to draw a line in the sand.</P>
<P>The announced layoffs are coming at the same time that Verizon is implementing a speedup, with suspensions and firings for workers who do not meet monthly quotas. "This is the most serious threat I've seen from the company," CWA Local 1109 President Tony Matarazzo told an emergency meeting of shop stewards.</P>
<P>The union hasn't seen a challenge like this since the company tried to force workers to pay for their health benefits in a bitter 17-week strike in 1989. The union beat the company at a time when most unions weren't winning strikes.</P>
<P>Workers should be prepared to dig in their heels again--the threat to our jobs is real. In addition to the strike deadline, the union has called for:</P>
<P>--Work-to-rule campaign, using the company's time-consuming safety and work guidelines and eliminating shortcuts.</P>
<P>--Publicity campaign to expose rotten plant conditions that the company refuses to fix.</P>
<P>--Grievances and elimination of subcontracting.</P>
<P>--An all-out effort for the mass rally at Verizon headquarters (42nd St. and 6th Ave.) on May 29 at 5 p.m.</P>
<P>The only way to take on the company and win is a fully mobilized and active membership. A strong showing on May 29 can take us a step in the direction of rebuilding our confidence in our most powerful weapon--ourselves. </P>

<P><font face="Times New Roman, Times, serif" size="4"><b>How we can organize to fight Verizon's layoffs</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE STRIKE deadline and mobilization are a welcome turnaround from the wait-and-see attitude that CWA leaders have taken in New York. Members in Local 1106 in Queens recently showed their dissatisfaction by rejecting a dues increase in a mail ballot. </P>
<P>Typically, the union beats its chest, but doesn't carry through with clear information and sustained action. And when the union has called action in the past, it often has not filtered down throughout the rank and file.</P>
<P>Some work locations have a very weak union presence, so job actions fall apart. Members feel like they are the only ones doing something--and then give up the fight for fear of being singled out. When union leaders do act, it is often due to pressure from the membership.</P>
<P>The mobilization for May 29 gives us an opportunity to change this situation--and strengthen our union. Within the rank and file, the difference between apathy and action is democratic self-organization. Stewards and union activists should mobilize workers in their shop on that basis and coordinate our actions.</P>
<P>A rank-and-file network could put out information, leaflet workplaces and win coworkers to the seriousness of the upcoming fight. It could also push for the termination of SEP as well as immediate union representation elections in Verizon Wireless as central demands for the current struggle and the 2003 contract campaign.</P>
<P>We can't rely on arbitration to protect our jobs. The decision could favor the company--then what?</P>
<P>We need to be prepared to strike to win our demands--and a rank-and-file network can help organize the tactics we need to win. </P>
<P>We have the power to fight and win--and save our jobs. This could show an example of how we can fight layoffs that are being carried out across the industry.</P>
<I><P>Verizon workers in the New York area who would like to help build a rank and file network at Verizon should email ranknfileverizon@hotmail.com for more information.</P></I>

