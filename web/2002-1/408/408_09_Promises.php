<P><font face="Times New Roman, Times, serif" size="5"><b>Children caught in the conflict</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Sherry Wolf</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 24, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>DOCUMENTARY: <I>Promises,</B></I> directed by B.Z. Goldberg, Justine Shapiro and Carlos Bolado.</P></font>
<font face="Times New Roman, Times, serif" size="3"><I><P>Promises</I> explores the Palestine-Israel conflict through the eyes of seven children who live minutes away from each other--but in separate worlds.</P>
<P>Between 1997 and 2000, before the current Intifada broke out, filmmaker B.Z. Goldberg spent time with the sons and daughters of Palestinians and Israelis. By the age of 11 or 12, these children have been immersed in the political debates and violence of life under the Oslo "peace" plan.</P>
<P>Moishe lives in the heavily guarded and gated West Bank Jewish settlement of Beit-El--which he describes as "a place where people who fight with Arabs live." His friend was killed by a "terrorist," and so he wishes that Arabs would all disappear.</P>
<P>The most interesting dynamic of the film develops between twin Jewish boys, Yarko and Daniel, and two Palestinian children their same age, Faraj and Sanabel, who live in the Dheisheh refugee camp, a 15-minute drive from the Jerusalem they've never seen.</P>
<P>The filmmakers take the twins to Dheisheh, where Faraj shows them how to use a slingshot, Sanabel shows them Palestinian dances, and they all spend the day playing like great friends. But at the end of the visit, Faraj cries as he says what they all know to be true--that they will go back to their separate worlds and may one day be enemies. </P>
<I><P>Promises</I> exposes the racist segregation of Israeli society, where Arabs are treated as criminals and endure the brutality of checkpoint guards and razor-wire fencing. But it seems to argue that if only Arabs and Jews would get to know one another as human beings, the violence would end. The children, like their parents before them, are pictured as individuals trapped in a cycle of violence beyond their control.</P>
<P>The film offers little insight into the political origins of the conflict--and concludes with a sense of hopelessness. Still, the movie provides glimpses of ordinary life in the Occupied Territories that Americans never see.</P>
<BR>
<font face="Times New Roman, Times, serif" size="4">Palestinian teen from <I>Promises</I> speaks out:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"Destroying hope in our hearts"</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>SANABEL AL-FARARJA is a 15-year-old Palestinian from the Dheisheh refugee camp who was featured in <I>Promises</I>. In March, she came to the U.S. to attend the Academy Awards, where <I>Promises</I> was nominated for best documentary film. But after Israel's assault on the West Bank took place, she was unable to return.</P>
<P>At the April 20 antiwar demonstration in Washington, D.C., Sanabel spoke with <I>Socialist Worker</I>'s PAIGE SARLIN.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">
<B><P>WHAT ARE your hopes for peace?</P>
</B><P>IF THERE is to be peace, we must return to our original towns. There are 8 million refugees waiting to return home to their villages. There must be no settlements. There must be no checkpoints to pass through in order to visit the capital of our country and no borders between us.</P>
<P>I think that under occupation, there is no peace, and our future is unknown. The Israeli occupation destroyed all of our dreams as it destroyed our homes--and any hope in our hearts.</P>
<B><P>DO YOU know what conditions are like in Dheisheh after Israel's assault?</P>
</B><P>I PHONED my family yesterday, and my mother says that the situation in our camp is very bad. No food, no medicine, no water. No electricity in all of Bethlehem.</P>
<P>My two-and-a-half-year-old sister told me, "There is some shooting at our house, Sanabel. I am afraid. I want you here with me." My mother told me that the Israeli soldiers have been shooting at our water tanks, so there is no water, and nobody can leave their houses.</P>
<P>A man from our camp went to bring bread for his children and medicine, and Israeli soldiers shot him with 30 bullets.</P>
<P>We are here to speak for our people and to tell of our suffering. I think many people here can understand us and stand with us.</P>
<B><P>WHAT DO you think of how the U.S. media have depicted the Palestinian struggle?</P>
</B><P>THE MEDIA doesn't show how the people and the children think. When I see the television here, I think we are the occupiers and not the Israelis. I think that Ayat Al-Akhras [an 18-year-old suicide bomber from Dheisheh] couldn't live this life, and she did this thing because of that.</P>
<P>The media ask me about suicide bombers, and they remember just five years. They don't remember 53 years under the occupation. They just remember the five years since the suicide bombings began.</P>
<P>Where are their minds? Where is their heart? What about the massacres in Jenin? The massacres in Bethlehem? </P>
<P>I hope to live in peace--to make our future, to be what we want and to have equal rights. I hope to live together with the Israelis. I am ready, but the Israeli government doesn't want that.</P>
<P>All the children are ready to live with the Israelis, but with equal rights.</P>

