<P><font face="Arial, Helvetica, sans-serif" size="2"><b>GERMANY</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Strikes put pressure on Schr&ouml;der for failing to deliver on promises</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Workers flex their muscle</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jeff Bale</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 24, 2002 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SUCCESSFUL STRIKES by metalworkers and election gains by the right wing are squeezing Germany's Social Democratic-led government.</P>
<P>The strike of 50,000 workers, organized by IG Metall, Germany's largest industrial union, was the first in seven years in the industry. Rolling walkouts were focused in Germany's industrial heartland in the southwest, mostly at auto plants like DaimlerChrysler and Audi.</P>
<P>Employers came up with a settlement, offering a 4 percent raise effective June 1, and a 3.1 percent increase a year later. This is below the union's original demands, but it was better than what the bosses had been offering.</P>
<P>The importance of the strike lies in its wider impact on German politics. On the one hand, pundits and politicians alike tried to use this strike to bash the long-standing system of national contract negotiations in Germany.</P>
<P>Since the Second World War, employers from an entire industry have sat down at the table with unions to settle on industry-wide contracts. This system has been credited with bringing German workers decent wages--and bringing bosses labor peace.</P>
<P>Now, with Germany's economy struggling, the system is being decried as outdated and a block to economic growth. IG Metall has been taken to task for demanding a wage increase while the economy sputters along with 1 percent growth, and business leaders have called for a weakening of labor laws.</P>
<P>The strikes also highlighted the political problems facing the ruling Social Democrat-Green coalition, which faces an uphill battle in national elections September 22. German Prime Minister Gerhard Schr&ouml;der has tried to follow the lead of Tony Blair's Labor Party in "modernizing" the party--that is, cutting as many ties to unions as possible, while currying favor with the bosses.</P>
<P>While Schr&ouml;der has run into much more opposition than Blair, his aim remains the same. Asked at a May Day rally about the impending strike, Schr&ouml;der essentially sided with the employers, declaring, "I expect support from the unions for a course of fairness and modernization." </P>
<P>In the days leading up to the strike, Schr&ouml;der sat down behind closed doors with the IG Metall leadership. "To be clear, I am winning this election," he told union leaders. "And if I don't, it's your fault." </P>
<P>Schr&ouml;der is right to be worried. The economy has been stagnant for the duration of his term, and his government has done little to improve the conditions of working people since it took office in 1997.</P>
<P>In the poverty-stricken state of Saxony Anhalt in the former East Germany, the Social Democrats' vote collapsed to 20 percent in last month's elections--a lower vote than the ex-Communist PDS party.</P>
<P>In addition, Schr&ouml;der has alienated a significant portion of the membership of the SPD in his eagerness to go to war in Afghanistan.</P>
<P>All this has given a boost to the conservative Christian Democrat candidate, Edmund Stoiber, who led Schr&ouml;der by 9 percent in an opinion poll in mid-May. Five years of "center-left" rule with nothing to show for it, a teetering economy and an organized far right have created the potential for another European election victory for conservatives.</P>
<P>Nevertheless, this metalworkers strike has given a big shot in the arm to the labor movement and the left. It can provide the basis for a left-wing alternative to Schr&ouml;der and his government.</P>

