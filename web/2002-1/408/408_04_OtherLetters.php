<P><a name="Justice"></a><font face="Times New Roman, Times, serif" size="4"><b>Where's justice in my case?</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I></B></P>
<P>On March 26, the First Circuit Federal Court of Appeals overturned my sentence of 10 years in prison for felony possession of a firearm. The opinion from the appeals court is a step towards justice, because I have been scapegoated for the death of Sgt. Cornel Young Jr. </P>
<P>Two white cops shot and killed off-duty Officer Young, who was Black. But I was sentenced to 10 years in federal prison for being a felon in possession of a firearm at the scene. </P>
<P>I was originally charged with murder, but the attorney general dropped the charge after public protests. Judge Lagueux said that if I hadn't brandished a weapon, Officer Young would be alive today. But I was on the ground and in police custody when Officer Young was killed!</P>
<P>The appeals court ruled that my actions did not result in his death, that his death was accidental, and that Judge Lagueux abused his discretion by sentencing me to the maximum sentence. But now, I go back in front of Judge Lagueux for resentencing. </P>
<P>I didn't hurt anybody. The police killed Officer Young, and I am in prison for it. Where's justice?</P>
<B><P>Aldrin Diaz,</B> Cranston, R.I.</P>

