<P><font face="Times New Roman, Times, serif" size="4"><b>An unjust peace suits Arafat just fine</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I></B></P>
<P>I believe that Eric Ruder missed the point in his article "Why did Arafat agree to an unjust peace?" (<I>SW, </I>March 29).</P>
<P>The PLO was supposed to be an active organization fighting against occupation and recruiting the Palestinian people into that struggle. But how is it at all conceivable that such a "revolutionary" organization can have the same leadership for more than three decades now? </P>
<P>Because Arafat was successful? Hardly. Rather, it was because the organization quickly became his personal fiefdom--along with a small circle of aides.</P>
<P>Well funded by ultra-conservative Arab oil sheikhdoms (what kind of liberation movement could that be?), Arafat and his chief PLO staff settled into a comfortable life of jet-setting and luxury accommodation. His mandate has never been democratically challenged. The Palestinian National Council, a sort of parliament in exile, was pruned of all dissent. </P>
<P>After Oslo, the remnants of the force that Arafat took out of Beirut eventually found their way to Sinai--where, under U.S. supervision, they were trained to become the Palestinian Authority security force, whose primary task would be to crush Palestinian resistance.</P>
<P>Arafat is a sham leader. As a figurehead he remains useful, and that is why the rank and file pay lip service to him. But he has no authority over his people and his orders (dictated by Israelis and Americans) are simply ignored.</P>
<P>So why did Arafat accept an "unjust" peace? Because it suited him. He doesn't really care. One of the first things he had built in Gaza was the VIP lounge at the new airport! This while most people do not have plumbing or sanitation.</P>
<P>Today, Arafat is the number one ally of Israel. If he was a real threat, they would have killed him years ago. Now that he has been "released," he must be looking forward to his next trip to Paris.</P>
<B><P>Tareq Ibrahim Ali,</B> London</P>

