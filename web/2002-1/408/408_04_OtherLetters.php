<P><a name="Kipnis"></a><font face="Times New Roman, Times, serif" size="4"><b>Paying tribute to Ira Kipnis</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I></B></P>
<P>Ira Kipnis, author of <I>The American Socialist Movement, 1897-1912,</I> died May 5 at his home in Chicago.</P>
<P>In 1953, soon after publishing what is still considered the best book on the Socialist Party, he was called to testify at Sen. Joseph McCarthy's hearings on communist activity at the University of Chicago, where he taught history. He lost his job after invoking his Fifth Amendment rights.</P>
<P>Not one to let adversity slow him down, he entered law school at the same university. Kipnis was an accomplished pianist and had a Masters degree in music from Northwestern University. He went on to become an authority on the author George Bernard Shaw--and in 1970 returned to the University of Chicago to teach Shaw and constitutional law.</P>
<P>Prior to his illness, Haymarket Books committed to republish Kipnis' book on the Socialist Party. That project will go ahead as a tribute to Ira Kipnis--and as an excellent resource for a new generation of socialists.</P>
<B><P>Bill Roberts, </B>Chicago</P>

