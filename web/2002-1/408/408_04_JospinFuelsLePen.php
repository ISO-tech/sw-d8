<P><font face="Times New Roman, Times, serif" size="5"><b>Jospin's policies gave Le Pen a lift</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 24, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>There are two main problems with Julien Ball's letter (<I>SW,</I> May 10) that criticized <I>SW</I>'s coverage of the rise of the French fascist Jean-Marie Le Pen. </P>
<P>First, it ignores the fact that Le Pen won the highest votes of any candidate in former <I>left-wing</I> working-class regions like Nord-Pas-de-Calais and Lorraine--votes that used to go to social democrats.</P>
<P>That 30 percent of manual workers and 38 percent of unemployed workers voted for the fascist candidate in the first round indicates that privatization, job cuts and the ensuing crime wave under Socialist Prime Minister Jospin's watch drove many to despair. </P>
<P>Second, there's nothing "intractable," as Ball puts it, about Le Pen's support. When unions led a mass strike against cuts in 1995, his support dissipated. Workers saw that a multiracial fightback could win. That's why the 1.5-million strong protests against Le Pen on May Day were an important step in posing an alternative to his hate. </P>
<P>Now, conservative President Chirac is adopting many of the social policies advanced by Le Pen. That's why the fight must continue against both the fascists and all the political parties--"left" and right--that help create an audience for Le Pen.</P>
<B><P>Sherry Wolf, </B>Chicago</P>

