<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">U.S.-Russia arms treaty:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"A worthless scrap of paper"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 24, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE NEW arms treaty between the U.S. and Russia will "liquidate the Cold War," says George W. Bush. But the deal won't make the world any safer--and it gives Washington a free hand to replace the Cold War with a series of hot ones.</P>
<P>The agreement cuts the number of strategic weapons on each side from 5,000 to 6,000 warheads each to 1,700 to 2,200. But that's still enough to blow up the world many times over.</P>
<P>And the U.S. can take nuclear warheads out of storage at any time to build up its forces. Russia, by contrast, is too broke to maintain its nukes and has no choice but to cut back.</P>
<P>"What we have now agreed to do under the treaty is what we wanted to do anyway," a top Bush administration official told the <I>New York Times</I>. "That's our kind of treaty."</P>
<P>Pavel Felgenhauer, an independent Russian defense analyst, pointed out other holes. "This treaty does not have any timetable for decommissioning [weapons], no definitions of what a warhead is or how to count them, no verification procedures--no nothing," he wrote. "Legally speaking, in military arms control terms, the new treaty is nothing more than a worthless scrap of paper."</P>
<P>The pact won't prevent the U.S. from withdrawing from a separate treaty in order to build a terrifying Star Wars spaced-based weapon system. And it won't stop the U.S. from developing "mini-nukes" as battlefield weapons.</P>
<P>What's more, the U.S. plans to expand NATO to include the three Baltic states that used to be part of the USSR--with a new Russia-NATO council as another sop to Russian President Vladimir Putin. For Putin, these deals are a way to save face as the U.S. steadily expands its military, political and economic influence across the former USSR.</P>
<P>After waging a barbaric war for control of Moscow's former puppet Afghanistan, the U.S. now has military installations in the five Central Asian states formerly run by Russia.</P>
<P>Washington justifies its aggressive new role as part of the "war on terror" and "defending democracy." But the fact is that the same Central Asian dictators who used to take their orders from Moscow now bow to Washington instead.</P>
<P>And the U.S. is willing to back these police states for control of what journalist Pepe Escobar calls "Pipelinestan"--the oil- and gas-rich region that runs from the Caspian Sea to Afghanistan.</P>
<P>"Pipelinestan is the golden future: a paradise of opportunity in the form of $5 trillion of oil and gas in the Caspian basin and the former Soviet republics of Central Asia," he wrote. "In Washington's global petrostrategy, this is supposed to be the end of America's oil dependence on the Organization of Petroleum Exporting Countries (OPEC)." Which of course is why Washington is targeting Iraq as well.</P>
<P>Finally, the idea that the U.S. is seriously interested in "arms control" is absurd in light of Bush's $400 billion military budget--which is just about equal to military spending by every other government in the world combined.</P>
<P>That's why we have to expose Bush's "peacemaker" pose for the public relations stunt it is--and renew our efforts to build a movement against the U.S. war machine.</P>

