<P><font face="Times New Roman, Times, serif" size="5"><b>Reports from the struggle</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 24, 2002 | Page 10</font></p>

<P><font face="Times New Roman, Times, serif" size="4"><b>Protest George W. Bush</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Keeanga-Yamahtta Taylor</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--About 150 protesters from around the state gathered to protest George W. Bush's visit May 13.</P>
<P>Bush came to promote his anti-poor "welfare" initiative and the "welfare-to-work" program at the Jefferson Street hub of United Parcel Service. He then made his way to downtown Chicago to attend a fundraiser for a Republican gubernatorial candidate.</P>
<P>Bush was met by protesters organized by the Chicago Coalition Against War and Racism. Demonstrators protested a wide range of Bush policies--from his endless war on the world, to the U.S. Navy presence in Vieques, to the U.S. funding of Israel.</P>
<P>Several speakers pointed out that Bush is willing to spend billions on war and on aid to Israel, yet three schools on Chicago's South Side have been closed.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Justice for Palestinians</b></font></P>
<font face="Times New Roman, Times, serif" size="3">
<P>ACTIVISTS IN cities across the U.S. turned out in recent days to call for an end to Israel's brutal occupation, even as Israeli Prime Minister Ariel Sharon promised more assaults on Palestinians.</P>
<P>On the West Coast, students at the University of California-Davis marked Palestine Awareness Week that began May 6. Sponsored by Students for Justice in Palestine (SJP), events included an Arabic hip-hop concert, Palestinian art displays, a documentary showing, a mock refugee camp and a panel discussion featuring Jewish students and activists who oppose the occupation.</P>
<P>"It improved a lot from last year because we're reaching out much more," Susan Abdallah, an organizer of the events, told <I>Socialist Worker</I>. SJP has grown in recent weeks, with chapters springing up on more than 25 campuses across the nation.</P>
<P>On May 15, members and supporters of the International Solidarity Movement (ISM) gathered for a press conference outside of the Israeli mission to the United Nations in New York City. The press conference was called to protest the arrest and deportation of 10 foreign activists by Israeli police. Among those who spoke were Bonnie Schurr, mother of arrested activist Kristen Schurr, and civil rights activist Dedrick Muhammad.</P>
<P>The activists were arrested for entering the Church of the Nativity in Bethlehem during the recent 39-day standoff between Palestinians and the Israeli military. They brought food, water and medicine to more than 130 Palestinians who had taken refuge inside. The Israeli government deported them--and has promised to bar their return.</P>
<P>Two days later, nearly 2,000 people marched in New York City to mark al-Nakbah, a commemoration of the destruction by Israel of hundreds of Palestinian villages and the expulsion of more than 800,000 Palestinians from their homes in 1948. Marchers chanted, "Not another penny, not another dime, no more money for Israel's crimes!" </P>
<P>The next night, May 18, more than 500 people turned out at the Good Shepherd Faith Presbyterian Church to hear members of the ISM speak about their recent experiences during the siege in Bethlehem. "We Palestinians have never been free, all our lives we are struggling for freedom, for liberty and justice," George Rishmawi of the Palestinian Center for Rapprochement told the crowd. "I can't accept that people who are defending their homes and families are called 'terrorists.'"</P>
<P> ISM member Adam Shapiro --whose family received death threats because of his activism--spoke about his commitment to resisting the occupation. "It's absolutely ludicrous to know that, because I'm an American, I can walk in the streets of Ramallah," he told the crowd. "But its citizens, the Palestinians, can't. Kids peek from behind the curtains into the empty streets and pull back quickly before the Israeli snipers kill them."</P>
<I><P>John Green, Ahmed Hawari, Pranav Jani and Lee Wengraf contributed to this report.</P>
