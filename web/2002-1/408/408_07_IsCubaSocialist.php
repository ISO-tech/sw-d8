<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Is Castro's Cuba a socialist society?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Bridget Broderick</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 24, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH White House wasn't long in responding to former President Jimmy Carter's controversial visit to Cuba. Three days after Carter returned, George W. Bush spoke out on Cuban Independence Day, spewing the same rabid anti-Castro rhetoric that the U.S. has used since the 1959 revolution that toppled the U.S.-backed dictator Fulgencio Batista.</P>
<P>No wonder. The Bush administration is packed with veteran Cuba haters--even a few exile fanatics like Otto Reich. These goons were furious about Carter's visit and his call for the U.S. to lift its economic blockade.</P>
<P>Carter couched his appeal in terms of helping ordinary Cubans, who have suffered enormously because of the embargo. But a section of Corporate America is beginning to adopt this position--and not because they're worried about Cuban living standards. Companies like DuPont and Monsanto want access to the Cuban market--and are tired of giving an advantage to Canadian and European competitors because of Washington's hatred of Castro.</P>
<P>The embargo should be lifted. The U.S. government shouldn't be able to impose misery on ordinary Cubans in order to promote U.S. interests in its "backyard."</P>
<P>But for many people who consider themselves radicals, Cuba is more than a victim of U.S. imperialism that should be defended against Washington. They believe that Cuba is socialist in some way--an alternative to capitalism that may be imperfect and embattled because of the U.S. siege, but an improvement nevertheless.</P>
<P>The revolution in 1959 rightly inspired people around the world because it was a blow against the long U.S. tradition of bullying and intervening in Latin America. But Castro and his allies didn't build a socialist society. </P>
<P>Though supported by most Cubans, the revolution didn't involve the masses of Cuban workers and the poor in their own liberation. The vast majority looked on as Castro and Ch&eacute; Guevara led a small guerrilla army to victory against Batista's corrupt regime.</P>
<P>The new government promoted literacy and health campaigns, and enacted land and housing reforms. But Castro said nothing about the new government being socialist for two years.</P>
<P>Only after the U.S. government made it clear that it wasn't about to accept any reforms in Cuba did the new government discover its "socialism"--not so coincidentally after it made an economic and political alliance with the former USSR.</P>
<P>Castro adopted the USSR's model for state-run industry and social services. The new system was called "socialist," but bore all the markings of capitalism--with a small minority at the top (in this case, state officials) making all the real decisions. Workers had no power over the direction or priorities of this state capitalist system. "Socialism" was imposed <I>on</I> Cuban workers--not created <I>by</I> them. </P>
<P>Many of Cuba's defenders insist that scarcity and tight government control are inevitable because of the U.S. embargo--and that the Castro government at least spends more for Cuba's social needs than the U.S. Both arguments are partly true. But they overlook important realities.</P>
<P>Many of the basic reforms won after the revolution have been undermined by the U.S. embargo--especially since Castro's adaptation to the free market during the 1990s "special period" following the fall of the ex-USSR. But the policies of the "special period" are only an extension of the top-down system that has existed all along, in which an elite has enjoyed special privileges and powers.</P>
<P>And while the Cuban government spends much more on health care and education for its citizens than the U.S. does, this could be said of many countries that aren't socialist--France, Germany and Canada among them. </P>
<P>Clearly, we don't side with Bush and the other anti-Castro fanatics. But socialists don't have to settle for Castro's fake "socialism" either.</P>
<P>A socialist society is about giving workers a say over the priorities of society--not leaving them in the hands of state bureaucrats.</P>

