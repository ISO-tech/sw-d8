<P><font face="Times New Roman, Times, serif" size="4">Teachers organize against boards' loyalty oaths</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"We say fight back!"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 24, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>I read with interest Sarah Knopp's report from Los Angeles about the devastating budget cuts going on in schools there (<I>SW,</I> May 17). One thing that caught my eye was the "loyalty oath" that she said teachers are being forced to sign. I had never heard of such a thing until last month, when the Washington, D.C., Board of Education tried the same thing here.</P>
<P>They passed an "emergency ruling" requiring every teacher to inform the board by April 30 of each year of their intent to stay in the school system. If a teacher failed to submit the intent form but reported for work in August, the board claimed the right to transfer the teacher to any school in need of staff.</P>
<P>Much more outrageous, though, were the consequences if a teacher failed to declare their intent to resign. The board had the gall to claim the right to revoke teachers' licenses, dock them any back pay and inform their new employer of "disloyalty" and "insubordination." </P>
<P>This draconian ruling came as a total shock to teachers. But neither we nor our union took this action lying down.</P>
<P>In an all-too-rare moment of action, the union leadership immediately drafted a letter denouncing this loyalty ruling--and urged building representatives to hold meetings to get every teacher to sign the letter and jam the board's fax machine with copies.</P>
<P>After receiving hundreds of letters from angry teachers, the board withdrew the ruling. The actions proved to many tired and frustrated teachers that we can win when we stick together.</P>
<P>The board claims that it passed this ruling to prevent the inevitable surprise each August, when dozens of teachers decide at the last minute that they can't take another year in D.C. schools.</P>
<P>Instead of slapping us in the face with these ridiculous "rules," maybe the board should take a look at why we are so fed up in the first place.</P>
<P>Sarah was dead on: They say cut back, we say fight back!</P>
<B><P>Jeff Bale,</B> Washington Teachers Union, AFT Local 6, Washington, D.C.</P>

