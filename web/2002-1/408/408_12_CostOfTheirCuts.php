<P><font face="Times New Roman, Times, serif" size="4">Florida girl lost in privatized system</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The cost of their cuts</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 24, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IT TOOK 15 months for Florida's Department of Children and Families (DCF) to discover that 5-year-old Rilya Wilson was missing from her foster home. But it took only a few minutes for Gov. Jeb Bush to blame DCF workers.</P>
<P>After he attacked caseworkers, Jeb then took a moment to use the tragedy to spout off about "family values." "The biggest issue here is the lack of wholesome love in family life in our state," said Bush. "To expect that the government can fill that void in a perfect fashion is impossible."</P>
<P>Rilya's disappearance is a tragedy and outrage. But it is a tragedy of Bush's own making. Since the president's little brother took office three years ago, he has turned crucial state services--from education to adoption--over to be administered by private companies.</P>
<P>And like his brother did as governor of Texas, Jeb wants to shift the job of providing for the poor to faith-based organizations. "State government can draw much from these reservoirs of faith," Jeb promised at his inauguration.</P>
<P>The governor wants to completely privatize Florida's child-welfare programs by 2003. Meanwhile, he's ignored the fact that Florida DCF caseworkers are overworked and overwhelmed--often handling twice as many cases as is federally recommended. "There is no place in the country where it's worse to be a foster child than Florida," said Richard Wexler, director of the National Coalition for Child Protection Reform.</P>
<P>Jeb has even more misery planned for poor and working people in Florida. Like state governments across the country facing the impact of the economic recession, Florida is using more outsourcing, privatization and cutbacks to balance its budget.</P>
<P>Jeb Bush's scheme comes down to four words: Make the poor pay. According to the Center on Budget and Policy Priorities, Florida plans to cut its budget by $1 billion. Among the reductions is another $4.7 million from child protection programs--making it more likely that there will be other cases like Rilya's.</P>
<P>Florida lawmakers have a lot of company as they line up to swing the budget ax. States across the country are making huge cuts to needed spending programs for everything from education to Medicare, the health care program for the elderly and disabled.</P>
<P>So in the richest nation in the world, children will sit in overcrowded classrooms, workers will go without medical care, and the elderly and disabled will be thrown to the wolves.</P>
<P>And in the weeks ahead, Washington plans to heap more punishment on the poor. New bipartisan welfare "reform" legislation would force recipients to work even harder and longer for their benefits.</P>
<P>Democrats like Sen. Hillary Clinton (D-N.Y.) claim to be standing up for the poor by throwing in a few more dollars in child care benefits. But their message to poor people is the same as the Republicans': Work or starve.</P>
<P>Meanwhile, Bush and the Pentagon are getting all the money they want for pet projects to fight the "war on terrorism."</P>
<P>No wonder people are getting more and more angry. Recent months have seen modest but widespread protests--from rallies against the cuts at city colleges in Chicago to a strike authorization vote by New York City teachers.</P>
<P>We have to link up these fights--and build a wider opposition to the bipartisan budget cutters and their assault on the poor.</P>

