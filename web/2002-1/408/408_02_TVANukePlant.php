<P><font face="Times New Roman, Times, serif" size="5"><b>Tennessee ready to restart broken nuke plant</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 24, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE TENNESSEE Valley Authority (TVA), long criticized as one of the worst polluters in the country, has come up with a new scheme. Fire up a run-down nuclear reactor.</P>
<P>Last week, the TVA's board voted to spend $1.7 billion to restart "Brown Unit One," one of three nuclear reactors located at Browns Ferry near Athens, Ala. All three reactors were shut down in 1985--when engineers at the public utility found out that their structures didn't match blueprints.</P>
<P>Two were restarted in the 1990s. Now the TVA wants to restart Unit One--because of "increased demand for energy."</P>
<P>Disgustingly, authority officials are trying to pass this off as an "environmentally friendly" way of producing energy. TVA board member Skila Harris--a former assistant to "environmentalist" Al Gore, no less--told reporters, "We must balance the responsibility to provide power to meet future needs with our objectives of protecting the environment and continuing the trend of debt reduction."</P>
<P>In fact, the TVA is already such a huge polluter that it can't get away with opening another coal-burning plant. And it would like to save some money as well.</P>
<P>"They're taking an old nuclear reactor that was not operated for 17 years, and they're going to run it longer and harder than it was designed for," said Stephen Smith, executive director of the Southern Alliance for Clean Energy. "It's a prescription for a serious problem."</P>

