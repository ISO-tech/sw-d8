<P><font face="Arial, Helvetica, sans-serif" size="2"><b>COLOMBIA</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Who's to blame for the violence?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By H&eacute;ctor Reyes</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 24, 2002 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE CIVIL war in Colombia took a sharp turn for the worse May 2 with the highest single-day civilian death toll in the war's history. Some 119 civilians--at least 40 of them children--died during a battle in the northwestern village of Bojay&aacute; between the left-wing guerrillas of the Revolutionary Armed Forces of Colombia (FARC) and right-wing death squad units known as paramilitaries.</P>
<P>Most of the civilians died when the FARC fired a primitive rocket that hit a church, near where paramilitaries had retreated. More than 300 of the village's residents had sought refuge in the church--and the rocket hit the building right above where they were huddled together.</P>
<P>Colombian President Andr&eacute;s Pastrana used the tragedy to justify his government's war on the FARC and other left-wing rebels--a war that the U.S. government backs to the hilt. "What happened was a massacre, a genocide of the FARC, who attacked the civilian population," he told the <I>New York Times</I>.</P>
<P>Yet both the United Nations and his own government's human rights office had warned more than a week in advance that a buildup of 300 paramilitaries near Bojay&aacute; had the potential of leading to a battle with significant civilian casualties. The human rights office even alerted the armed forces, national police, interior ministry and vice president's office.</P>
<P>But no one lifted a finger to protect the civilians. In fact, Pastrana's government is far from blameless.</P>
<P>The Colombian military is directly tied to the paramilitaries. These thugs serve as a private security force for right-wing politicians, big landowners and ranchers--for the purpose of drug trafficking, land grabbing and exploitation of peasants and workers.</P>
<P>According to organizations like Human Rights Watch, the paramilitaries are responsible for the vast majority of civilian deaths in Colombia's four-decade-old civil war, including the systematic murder of unionists.</P>
<P>Resistance to these killers is entirely justified. Nevertheless, the FARC's tactics must be criticized. Its military strategy at best ignores the role of the millions of ordinary Colombians in fighting for their rights--to the detriment of building the popular and working-class movements. At worst, ordinary people become unintended but inevitable targets in the FARC's battles with the Colombian army and the paramilitaries.</P>
<P>In a press release apologizing for the attack on Bojay&aacute;, a FARC commander said only, "We are an irregular army, and we don't have weapons as sophisticated as those of the regular army. To hit a target with a cylinder [rocket], we have to try three times." This statement reveals the FARC's lack of understanding that ordinary Colombians need to be involved in the fight for liberation.</P>
<P>The Bush administration is certain to use the Bojay&aacute; tragedy to make its case for deeper U.S. intervention. The State Department recently cleared the way for an additional $62 million in military aid after claiming that Colombia's military has made "improvements" in breaking its links to the paramilitaries.</P>
<P>And as <I>Socialist Worker</I> goes to press, Congress is considering a Pentagon spending authorization bill that will remove the current limit on how many U.S. military personnel can be deployed in Colombia.</P>
<P>Another piece of legislation likely to be passed as part of an appropriations bill would justify U.S. intervention in Colombia as part of the "war on terror"--in addition to the "war on drugs." This is a sick joke.</P>
<P>We need to be clear that U.S. intervention in Colombia--either military or economic--will only cause more civilian death and misery. We must demand an end to Washington's support for Colombia's dirty war.</P>

