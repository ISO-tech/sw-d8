<font face="Times New Roman, Times, serif" size="4"><P>Latest agreement opens door to concessions<BR></font>
<font face="Times New Roman, Times, serif" size="5"><b>IAM pushes contract at United</P></b></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Todd Chretien</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 8, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MECHANICS AT United Airlines were set to vote March 5 on a contract that both the company and union leaders call an "industry leader" but that in fact opens the way to concessions.</P>
<P>The contract does win gains on health care and pensions. But a number of rank-and-file members in International Association of Machinists (IAM) District 141M oppose the deal because it allows concessions in the event of a "financial recovery plan" instituted by management.</P>
<P>IAM members rejected a previous deal proposed by George W. Bush's Presidential Emergency Board with a 68 percent "no" vote in mid-February because it included language committing the IAM to take concessions.</P>
<P>The IAM leadership and United claim that the new tentative agreement removes the concession language and will give mechanics a 37 percent pay hike over the life of the contract and thousands of dollars in retroactive pay.</P>
<P>Union leaders also claim that United has no intention of asking for any future concessions and that workers will have the right to vote "no" if they do.</P>
<P>But the devil is in the details--including an opening for outsourcing of union work and even a rollback of the pay increase in just a few months.</P>
<P>At a contract informational meeting in San Francisco's Local Lodge 1781, United mechanic and shop steward Jennifer Biddle asked union leaders what would stop the company from outsourcing heavy maintenance overseas.</P>
<P>All IAM District 141M President Scotty Ford said is "That's a good question"--and then insisted that United wouldn't risk making mechanics angry with such actions.</P>
<P>But simply relying on United to keep its word is foolhardy. After taking a pay cut in 1994 in exchange for an employee stock ownership plan (ESOP), mechanics have gone nearly a decade without a pay raise.</P>
<P>United has dragged on contract negotiations for two years--and after September 11, the company laid off 20 percent of the workforce and imposed a schedule of six days on and two days off for mechanics.</P>
<P>While claiming that the contract is a big win, IAM leaders have failed to actually give the membership an adequate opportunity to discuss the details. Union officials even signed a "letter of agreement" with the company promising to make their "best efforts to obtain membership ratification" of the contract.</P>
<P>Informational meetings were lightly publicized and attended by only several hundred workers in San Francisco, where United's biggest maintenance center is based. Outrageously, IAM leaders have stooped to harassing rank and filers who vocally opposed the contract.</P>
<P>The IAM's willingness to accept a contract that allows United to impose concessions as soon as the ink is dry on the agreement--and their dubious methods in trying to get that contract passed--have created enormous resentment in the rank and file.</P>
<P>Unfortunately, much of that anger is being channeled into an effort to decertify the IAM and replace it with the Aircraft Mechanics Fraternal Association, a craft union that would divide the skilled mechanics and other less skilled IAM members such as baggage handlers into separate unions.</P>
<P>Pro-AMFA activists claim that AMFA will be more democratic and responsive to the skilled mechanics' demands. Some argue that workers can vote for the contract now and then vote for AMFA in a representation election later this year in order to resist concessions.</P>
<P>Yet United will use the division of its most powerful union into two competing, hostile parts as a wedge to destroy solidarity and cut the standard of living and working conditions of both. And at its worst, AMFA plays on racist, anti-immigrant prejudice against nonskilled mechanics in the IAM.</P>
<P>It would be a tragedy if rank and filers harness their legitimate anger at the IAM leadership into splitting the union instead of waging a fight against concessions alongside all the unions at United Airlines.</P>

