<P><A HREF="397_06_TouficTikva.shtml"><font face="Times New Roman, Times, serif" size="4"><b>"Everything is a struggle"</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">Toufic Haddad is a Palestinian activist based in Ramallah, and Tikva Honig-Parnass is an Israeli activist who lives in Jerusalem. They co-edit the left-wing journal <I>Between the Lines.</I> Here, they talk about the struggle in Palestine in the face of Israel's escalating violence.</P>

<P><A HREF="397_06_AsafOron.shtml"><font face="Times New Roman, Times, serif" size="4"><b>"The right-wingers see me as a traitor"</b></font></A><BR>
