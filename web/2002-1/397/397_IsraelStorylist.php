<P><A HREF="397_06_SJPDivestment.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Cut the ties to Israel's apartheid</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">Students for Justice in Palestine, an activist group at the University of California-Berkeley, hosted a national solidarity conference in mid-February of some 400 people. One of the main strategies coming out of the conference is a national campaign to pressure colleges and universities to divest from Israel.</P>

