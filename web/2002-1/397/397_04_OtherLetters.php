<p><font face="Times New Roman, Times, serif" size="4"><b>UAW getting hit by layoffs in Ohio, too</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>I read your story about Ford closing plants and layoffs. We are getting the same thing here in Springfield, Ohio.</P>
<P>The problem is that they built a new assembly plant in Escobedo, Mexico, and are ramping up production there.</P>
<P>They put all the stories in the local paper about the economy being bad and orders being low. If we had the orders back from Mexico we would have plenty of work.</P>
<P>Our local union says they have complained to the international union. We are still getting laid off.</P>
<P>I am just 300 people away from being laid off, in spite of 31 years of service. I need two more years to get a $2,000 retirement benefit, but I don't see that happening if things keep going south.</P>
<P>I belong to Local 402 of the United Auto Workers and have asked about whether our laid-off people will get Trade Adjustment Assistance benefits from the North American Free Trade Agreement (NAFTA). But all they say is that this is "very complicated" and that they will get back to us.</P>
<B><P>Bob Layne,</B> Springfield, Ohio</P>

