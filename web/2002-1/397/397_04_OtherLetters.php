<p><a name="mistreating"></a><font face="Times New Roman, Times, serif" size="4"><b>A long history of mistreating people</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>The U.S. has a long history of mistreating people from different ethnic backgrounds, nationalities and religions.</P>
<P>It began with the mistreatment of African Americans during the period of slavery. During the Second World War, Japanese Americans were held captive in internment camps. During the Gulf War, some captured Iraqi soldiers were buried alive. Now the U.S. is being criticized by the world community for our inhumane treatment of Afghan prisoners held in Cuba.</P>
<P>During the Vietnam War, when American soldiers were held captive by the Vietnamese, the U.S. "reminded" Vietnam of the Geneva convention. Now, since the U.S. has failed to treat Afghan prisoners properly, people abroad are reminding us.</P>
<P>At home, we treat Arab and Muslim Americans in the worst way possible. Hundreds have been held since September 11 without bail, and there are reports that some detainees have been tortured.</P>
<P>Their family members have held demonstrations and demanded answers. But the FBI has "no comment."</P>
<P>What will be the fate of our service men and women, sent to war by our warmongering politicians, if they are captured on foreign soil? Will they be treated in a humane way?</P>
<P>Does the White House care about that? Of course not.</P>
<B><P>Hasan,</B> New York City</P>

