<p><a name="Boeing"></a><font face="Times New Roman, Times, serif" size="4"><b>Boeing workers need job security</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Here at Boeing, pensions are the number one issue. The union has not produced any results on job security, and the average age of workers is 45 years. We are out of time and must fix this now. Union officials make around $6,000 a month on pensions, and we make $1,500 at best.</P>
<P>Job seniority language also needs massive overhaul. Tooling workers with 24 years of experience are going out the door, while mechanics with two to three years stay. This is immoral.</P>
<P>I've been pursuing the International Association of Machinists (IAM) for more than eight months to obtain their Pension Plan documents. I am now contacting the Department of Labor Pension and Benefits Administration for help because the IAM is keeping mum on the issue.</P>
<B><P>Don Grinde, </B>Marysville, Wash.</P>

