<font face="Times New Roman, Times, serif" size="5"><b><P>"We should be treated like men"</P></b></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Mikki Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 8, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>TELEVISION: <I>10,000 Black Men Named George,</B></I> a Showtime original movie, directed by Robert Townsend, starring Andr&eacute; Braugher, Charles S. Dutton and Mario Van Peebles. Showing March 7.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>THE SHOWTIME movie <I>10,000 Black Men Named George</I> chronicles Black Pullman workers' 10-year battle for a union, the Brotherhood of Sleeping Car Porters.</P>
<P>The title comes from the degrading practice of calling porters "George," after company founder George Pullman.</P>
<P>In 1925, porters approached A. Phillip Randolph (Andr&eacute; Braugher), Black socialist editor of <I>The Messenger</I>, about organizing an independent union. Randolph--unsuccessful in previous attempts to organize Black workers--at first refused, but porters eventually convinced him to lead the effort.</P>
<P>The first union meeting, held in Harlem, drew 500 people. Randolph laid out demands for fair wages and "by no means least, that porters be treated like men." More than 200 porters flooded Randolph's office the next day to sign union cards.</P>
<P>The Brotherhood began an organizing tour of Northern cities, and built an underground network in the Jim Crow South. By the end of 1926, the union included more than half the porters, but Pullman still refused to recognize it.</P>
<I><P>10,000 Black Men</I> does an excellent job portraying Pullman's viciousness. It fired porters suspected of union activity and spent vast sums on an army of spies and thugs and an anti-Communist scare campaign.</P>
<P>In 1928, with membership and funds dwindling, Randolph called for a strike vote, hoping the threat alone would force Pullman to the table. But, despite porters' 6,053-to-17 vote to strike, Randolph called off the walkout on the advice of American Federation of Labor President William Green. This demoralized the porters, who were ready to lay everything, including their lives, on the line for their union.</P>
<P>The Brotherhood was all but dead by 1932. But in 1933, new federal legislation that made organizing easier re-energized the union.</P>
<P>Sensing new dangers, Pullman fired hundreds of porters and formed a competing "independent" union. But Pullman's "union" was voted down by a 6-to-1 margin, and the Brotherhood became the official porters' union in 1935.</P>
<P>While the movie ends here, it took two more years before Pullman signed a contract with the porters--the first ever between a union of Black workers and a major U.S. company.</P>
<I><P>10,000 Black Men</I> is refreshing in its unapologetic pro-union stance and its portrayal of the determination of ordinary people to stand up to one of the biggest, meanest companies.</P>
<P>One weakness is its uncritical portrayal of Randolph, though his faith in "working the system" and not in the power of the rank and file merits a critical look.</P>
<P>Still, people should see <I>10,000 Black Men</I>--and learn more about the historic struggles of Black workers in the U.S.</P>

