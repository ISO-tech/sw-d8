<font face="Times New Roman, Times, serif" size="5"><b>Stop the IMF and World Bank!</P></b></font>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 8, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GLOBAL JUSTICE protesters who marched against the bosses' World Economic Forum summit in New York City were right to declare: "They are Enron, we are Argentina."</P>
<P>In April, we have another opportunity to show our solidarity with the struggle in Argentina. The Mobilization for Global Justice in Washington, D.C., last week issued a call for demonstrations against the IMF and World Bank--the international financial institutions responsible for free-market disasters like Argentina's--during their annual spring meetings on the weekend of April 19-21.</P>
<P>That same weekend, School of the Americas Watch is planning a demonstration against Plan Colombia, and the National Youth and Student Peace Coalition has called an antiwar demonstration.</P>
<P>The fact that these organizations are working together for protests to challenge both the military and economic faces of U.S. dominance is a step forward for both the global justice and antiwar movements.</P>
<P>Activists now have a focus for organizing over the coming months. We need to begin building locally to turn out as many people as possible for April 19-21.</P>
<P>It's time to send a message to Washington.</P>

