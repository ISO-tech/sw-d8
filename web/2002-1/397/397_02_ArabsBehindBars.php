<P><font face="Times New Roman, Times, serif" size="5"><b>Behind bars for being Arab</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 8, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>HUMAN RIGHTS groups believe the number of people detained in the U.S. in the investigation into the September 11 attacks could be as high as 2,000. Of course, that's just an estimate--because John Ashcroft's Justice Department stopped keeping count in November.</P>
<P>They won't even tell Rabih Haddad why he's being held. A founding member of the Muslim charity Global Relief Foundation, Haddad was taken from his house in Ann Arbor, Mich., on December 14.</P>
<P>According to Britain's <I>Independent</I> newspaper, he's kept in his cell 23 hours a day. When he's allowed out, Haddad is kept handcuffed for the entire time, even while he showers--which he's allowed to do just three times a week.</P>
<P>The Justice Department hasn't offered any evidence to link Haddad to September 11 or any terrorist activity. But he's stuck behind bars indefinitely. And now, his wife and three of their four children have been served with deportation orders.</P>
<P>We have to stand up against this repression--like the Chicago activists who organized a picket to show their support for Haddad last month.</P>
<P>Don't let them take away our rights!</P>

