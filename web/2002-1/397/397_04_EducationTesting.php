<P><font face="Times New Roman, Times, serif" size="5"><b>These tests only show who's rich and who's poor</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 8, 2001 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Annie Levin's expos&eacute; of education "reform" (<I>SW</I>, February 22) hit the nail on the head. The Rhode Island Department of Education recently released the results of statewide math, English and health assessments that showed--surprise--that the rich suburban districts did well, while the urban districts did poorly.</P>
<P>The local media, while shaking their heads at the deplorable state of urban education, forgot to mention one thing. One of the side effects of poverty on schools--and the illness, instability and degradation that come with it--is a higher absentee rate. Students who were absent on the day of the test received a zero.</P>
<P>Put bluntly, these tests were an excellent measure of who's rich and who's poor--and reflect nothing about the quality of education or of the students.</P>
<P>These results, of course, were used to say that "education reform" needs to be sped up in Rhode Island's urban schools.</P>
<P>Meanwhile, Providence, R.I., teachers--who have been working without a contract since September--were accused of being "an obstacle to reform" because they rejected a contract offer that would have taken more power from their hands and put it into those of the dictatorial superintendent.</P>
<P>The real responsibility for the sad state of our schools lies not with the students, parents and teachers, who are under daily attack in this system. It lies with the politicians and the corporations, who benefit from under-educating--and blaming--us.</P>
<B><P>Brian Chidester, </B>Bristol Warren Education Association, Bristol, R.I.</P>

