<P><font face="Times New Roman, Times, serif" size="5"><b>Legal bribes line the politicians' pockets</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 8, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>EVEN AS they solemnly debate "reforms" to the campaign finance system, leading members of Congress have a new scam for raking in even bigger bucks from Corporate America.</P>
<P>According to a study released last week by the watchdog group Public Citizen, corporations are using a little-known loophole to directly line the pockets of Washington's biggest operators.</P>
<P>Direct donations to politicians are limited under the law, but corporations get around this by making unlimited "soft-money" contributions. Usually, soft money goes to the two mainstream parties.</P>
<P>But politicians can set up their own organizations--known as "527 groups," after Section 527 of the federal government's tax code--to personally accept soft-money bribes.</P>
<P>Public Citizen researchers found that the 25 biggest 527 groups run by federal officeholders raked in more than $15.1 million in the year ending June 30, 2001, almost all of it from corporations.</P>
<P>Joan Claybrook, the president of Public Citizen, said that 527 groups are a "mechanism for legalized bribery. They allow corporations to put unlimited amounts of money directly into the pockets of members of Congress. This corporate investment is for one purpose only: to shape the laws that Congress votes on and ultimately approves."</P>

