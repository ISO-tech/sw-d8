<font face="Arial, Helvetica, sans-serif" size="2"><b><P>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>How do struggles lead to revolution?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 8, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WE ARE often confronted, as Marxists, with the question of how society can change in a revolutionary way.</P>
<P>In periods like today, where the level of struggle still remains quite low (in terms of strikes, for example, or mass protests), socialists must continually argue with people not only that change is possible, but the struggle to achieve it is possible.</P>
<P>It's not difficult to tap into the class anger that people feel against the greedy rich--I was told that at a recent <I>Socialist Worker</I> paper sale, more than one passerby expressed the idea that "they should take the money of the Enron execs and give it to the ex-Enron employees."</P>
<P>It's not too great a leap from this to the instinctively socialist idea that we should take the wealth of the minority who are the filthy rich--wealth they squeezed from the labor of workers--and redistribute it to the working-class majority.</P>
<P>What's missing is the idea that such a thought could be anything but that--a passing thought, not a practical, realizable goal.</P>
<P>In fact, such a thing could not be implemented without workers seizing political power, taking control of production and reorganizing it to meet human need. A revolutionary movement capable of carrying this out would have to embrace hundreds of thousands of workers, who would need to lead millions more.</P>
<P>Such a movement doesn't spring up out of nowhere, but out of smaller, more prosaic struggles that build the confidence of workers that they can, by their own action, make change.</P>
<P>Struggles are forced upon workers even if they are initially reluctant to do battle--out of exasperation and a sense that there is no other choice. But out of struggles--over things like wage rates, work hours, conditions on the job, sexual harassment or racism by a supervisor--come a stronger sense of class consciousness ("us versus them") and a greater confidence.</P>
<P>It's only through such struggles that workers are able to shake off the ideas propagated in the schools, in the press, on TV and in Hollywood--of racism, sexism and anti-immigrant sentiment, or the idea that ordinary people are too divided and ignorant to change things.</P>
<P>Of course, revolution isn't made possible simply by an accumulation of small struggles. Sometimes there are qualitative leaps, where the struggle jumps to a new level. These leaps happen in terms of the struggle itself, and in the consciousness of its participants.</P>
<P>Elected strike committees (at least some of the time) are commonly created to run strikes in the U.S. Often, they are nothing more than a means to operate a single strike in a single workplace--to organize picket duty, provide food and relief to strikers, and so on.</P>
<P>The sense of class solidarity and power created by a general strike is qualitatively higher than a strike in a single workplace. For example, in Seattle in 1919, almost all the city's workers struck in solidarity with 35,000 longshore workers who had previously struck for higher pay.</P>
<P>A strike committee was formed of elected representatives from each of the 101 striking union locals. The strike committee discussed not only issues of how to conduct the strike, but how to keep basic services running during the strike.</P>
<P>"Labor will not only SHUT DOWN the industries," explained an editorial in a Seattle paper on the eve of the strike, "but labor will REOPEN, under the management of the appropriate trades, such activities as are needed to preserve public health and public peace."</P>
<P>"If the strike continues," the article continued, "Labor may feel led to avoid public suffering by reopening more and more activities, UNDER ITS OWN MANAGEMENT."</P>
<P>The strike ended, under pressure from conservative union leaders, in six days. Nevertheless, it raised in embryo the question of <I>power</I>--of who runs society and in what way.</P>

