<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 8, 2002 | Page 11</font></p>

<p><font face="Times New Roman, Times, serif" size="4"><b><P>Washington, D.C., public schools</P></b></font>
<font face="Times New Roman, Times, serif" size="2"><b><P>By Bruce Cooley</P></b></font>
<font face="Times New Roman, Times, serif" size="3"><P>WASHINGTON, D.C.--Maintenance and building workers at D.C. Public Schools (DCPS) are launching a fight against the school board.</P>
<P>The board wants to spend less to clean, heat and maintain the city's public schools--despite a federal study that found DCPS buildings topping the list of the nation's school districts that need major repairs or complete replacement.</P>
<P>And despite understaffing, they're planning to lay off maintenance workers, forcing many building and maintenance workers to re-apply for jobs they already have.</P>
<P>The cuts are a consequence of money gone missing. After a budget surplus last fall, DCPS reportedly now faces a shortfall of up to $80 million.</P>
<P>Nearly 100 workers, members of the International Brotherhood of Teamsters (IBT), rallied in mid-February before taking their message to a school board meeting.</P>
<P>Workers and IBT leaders expressed outrage at the cuts and the school board's callous disregard for public education. "You didn't lose that money, but you're being forced to pay for it," Rev. Graylan Hagler, a civil rights activist, told the crowd.</P>
<P>Workers, union leaders, public school students and teachers addressed the rally. "We need more workers, not less," IBT Local 730 President Archie Smith told <I>Socialist Worker</I>. "These workers are already on a shoestring budget and have more buildings than they can take care of."</P>
<P>Many pointed out the need for increased solidarity among the city's unions in this fight. "We've got to get into the streets," Smith told <I>Socialist Worker</I>. "And we need the support of all the city's workers because they're the parents of the kids in these schools."</P>

