<p><font face="Times New Roman, Times, serif" size="4"><b><P>World Trade Center Marriott Hotel</P></b></font>
<font face="Times New Roman, Times, serif" size="2"><b><P>By Michael Ware</P></b></font>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Workers at the former World Trade Center (WTC) Marriott Hotel rally every Wednesday in Times Square to demand that Marriott make good on its promise to rehire all workers laid off after September 11. "Boycott Marriott!" chant the protesters.</P>
<P>Although Marriott has received millions of dollars from funds to "rebuild New York" and is opening two new hotels nearby, they're refusing to rehire WTC employees. So much for "United We Stand."</P>
<P>Marriot is one of the world's biggest hotel chains with 2,300 properties and $10 billion in sales. "Marriott wants new workers for cheap," Amy Velez, an activist with Center for Immigrant Families, told <I>Socialist Worker</I>. "Instead of $17 an hour for a worker with seniority, they want to pay $11 an hour for somebody new."</P>
<P>The 500 laid-off workers are represented by Hotel Employees and Restaurant Employees Union Local 6, which has yet to distribute the relief funds they have received for laid-off hotel workers.</P>
<P>Most of the workers are still unemployed but are determined to keep fighting until they are rehired by the hotel.</P>
