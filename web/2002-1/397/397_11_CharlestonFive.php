<P><font face="Times New Roman, Times, serif" size="5"><b>Charleston Five celebrate win</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 8, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHARLESTON, S.C.--More than 250 people turned out here to celebrate the freedom of the Charleston Five, one of labor's biggest political victories in decades. The rally, held in ILA Local 1422's new union hall, attracted union leaders and activists from across the U.S. and as far away as Sweden and Spain.</P>
<P>With quotations from the great Black antislavery leader Frederick Douglass and the song "We Are the Union," the celebration sounded the themes of the civil rights movement and the traditions of labor solidarity that inspired the struggle.</P>
<P>Cheers erupted during a video about the defense campaign that included scenes of the electrifying rally of 7,000 at the South Carolina statehouse.</P>
<P>The struggle of the Charleston Five began after heavily armed riot police attacked an International Longshoremen's Association (ILA) picket line of a nonunion ship owned by Denmark-based Nordana Lines in January 2000.</P>
<P>South Carolina State Attorney General Charlie Condon--in a race-baiting campaign against the overwhelmingly African American union--bragged that he would put union members "under the jail."</P>
<P>He put five members of ILA Locals 1422 and 1771--Kenneth Jefferson, Elijah Ford Jr., Peter Washington Jr., Ricky Simmons and Jason Edgerton--under house arrest for 20 months while they faced felony riot charges that carried prison sentences of up to five years.</P>
<P>But in scores of union meetings and labor rallies across the U.S. and abroad, ILA Local 1422 President Ken Riley publicized the case and raised funds. AFL-CIO President John Sweeney gave his official support--but rank-and file union activists and grassroots local committees were crucial to the struggle.</P>
<P>The West Coast-based International Longshore and Warehouse Union (ILWU) provided crucial funds early on--and ILWU President James Spinosa presented a check for an additional $167,000 at the victory rally. ILA President John Bowers, also on hand, promised to finally release to Local 1422 "every dime" of the more than $100,000 collected in his separate fundraising efforts.</P>
<P>The money is needed because Local 1422 and more than two dozen of its members still face a civil lawsuit by the nonunion company.</P>
<P>Also in attendance were leaders of unions affiliated with the International Dockworkers Council (IDC), whose members vowed to shut down ports around the world if the five went to trial.</P>
<P>At the victory rally, IDC coordinator and Spanish dockworkers' leader Juli&aacute;n Garc&iacute;a presented Riley with a framed copy of the letter sent to the captain of a Nordana ship, refusing to unload its cargo anywhere in Spain until Nordana agreed to use union labor in Charleston.</P>
<P>"This struggle of the dockworkers began in Liverpool, where the union was broken," Garc&iacute;a told <I>Socialist Worker</I>. "We are here to continue that struggle."</P>

