<p><font face="Times New Roman, Times, serif" size="4"><b><P>Stop repression in Zimbabwe</P></b></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Laura Post Durkay</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--About 15 people picketed outside the Zimbabwean mission to the United Nations on March 1. Protesters handed out informational leaflets to passers-by and chanted "Free speech, free elections! Hey Mugabe, stop the repression!"</P>
<P>There was no response from inside the mission, so we papered the front of the building with flyers, chanting that we'd be back.</P>

