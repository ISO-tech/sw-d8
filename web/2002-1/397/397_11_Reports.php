<p><font face="Times New Roman, Times, serif" size="4"><b><P>Fighting for immigrant rights in San Diego</P></b></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Justin Akers</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SAN DIEGO--About 120 protested the February 25 arrest of well-known immigrant rights activist Benjamin Prado, coordinator of the Raza Rights Coalition (RRC).</P>
<P>The assault and arrest are the result of a new joint exercise between the U.S. Border Patrol and the Metropolitan Transit Authority (MTA), a private company that operates San Diego's trolley lines.</P>
<P>The two are collaborating on a new program to "crack down" on undocumented immigrants who use the trolley lines for transportation.</P>
<P>Latinos make up about 60 percent of trolley passengers and mainly use trolleys for transport to work and school. This program allows the Border Patrol to freely harass any passenger they suspect of "looking illegal" and to arrest them.</P>
<P>Benjamin Prado and other RRC activists decided to take a stand against the racist abuse of innocent people by videotaping the Border Patrol "at work." One segment showed eight people detained in 30 minutes.</P>
<P>The Border Patrol--emboldened by the general crackdown on immigrant rights since September 11--decided to strike back. After filming a segment and stepping off the trolley, Prado was attacked by six uniformed officers. Witnesses said Prado did nothing to provoke them.</P>
<P>The officers proceeded to beat Prado, smash his camera and take him to San Diego's federal jail for "resisting arrest" and "interfering with a federal officer."</P>
<P>Fortunately, our protest and media attention forced the release of Prado that same evening--and activists vowed to continue organizing against Border Patrol harassment on trolleys.</P>
<P>By working together, our community can beat back injustice.&#9;</P>

