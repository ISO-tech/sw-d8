<P><font face="Times New Roman, Times, serif" size="5"><b>Cut the ties to Israel's apartheid</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 8, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>SNEHAL SHINGAVI, a member of Students for Justice in Palestine at UC-Berkeley, talks about how activists in the U.S. can support the struggle in Palestine.</P>
</B><P>STUDENTS FOR Justice in Palestine--an activist group at UC-Berkeley--hosted a national solidarity conference in mid-February that brought out some 400 people. One of the main strategies coming out of the conference is a national campaign to pressure colleges and universities to divest from Israel.</P>
<P>Divestment as a political strategy was used in the 1980s during the movement against apartheid in South Africa. Students realized that their universities were shareholders and even corporate partners with companies that profited from the racist policies of South Africa.</P>
<P>The demand that colleges and universities sell their holdings in these companies became a way to mobilize students on different campuses around a concrete political focus. And it helped educate a generation of activists about the South African struggle for liberation.</P>
<P>Like Black South Africans under apartheid, Palestinians don't have even basic rights--the right to protest, freedom of movement, housing rights. And they suffer from high levels of racism, poverty and unemployment under the domination of the Israeli government.</P>
<P>Not surprisingly, U.S. institutions of higher education today have all sorts of ties to Israeli apartheid. The University of California system, for example, has millions of dollars invested in General Electric (GE), Raytheon, Hewlett-Packard and Ericsson.</P>
<P>These companies all produce military equipment for the Israel Defense Forces--equipment used to demolish homes, set up checkpoints and carry out assassinations of Palestinian militants.</P>
<P>But the list of companies isn't limited to weapons manufacturers. Major corporations like AOL Time Warner and IBM also have substantial amounts of money invested in Israel.</P>
<P>Much of the $6 billion annual U.S. aid package for Israel comes with the stipulation that it be used to purchase U.S. goods. And the U.S. sends high-level officials--like former Secretary of State Madeleine Albright--to Israel to negotiate contracts on behalf of corporations like Boeing and GE.</P>
<P>Practically every college and university invests in some company that does business in Israel.</P>
<P>In April of last year, more than 300 students at UC-Berkeley took part in a building occupation to demand that the school divest from Israel. These kinds of actions are an important contribution to the struggle for justice in Palestine.</P>
