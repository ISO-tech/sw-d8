<p><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A "recovery" that workers can't feel</b></font></p>
<p><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 8, 2002 | Page 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><p>"THE U.S. economy appears to be steaming out of recession," rejoiced the <i>Wall Street Journal</i> this week. The <i>New York Times</i> went a step further, arguing, "And if that is not enough reason to break out the champagne, consider this. There is now some question whether the country was ever really in a recession."</p>
<p>Tell that to the more than 1 million workers who lost their jobs last year. The official unemployment rate dropped in January to 5.6 percent from 5.8 percent in December, but that statistic doesn't mean that more workers are finding jobs. According to the Bureau of Labor Statistics, the decline reflects only that "discouraged job seekers are giving up, or not beginning, their search."</p>
<p>By the end of 2001, the unemployment rate for Black workers stood at 10.2 percent; for Latino workers, it was 9 percent. And whether or not the recession is winding down, unemployment is still winding up--and could reach 6.5 percent overall by the end of this year, according to the Economic Policy Institute (EPI).</p>
<p>By the end of 2002, the EPI predicts that Black teenage unemployment could reach the Depression-era level of 35.3 percent--while joblessness among women heads of households could increase to 8.6 percent.</p>
<p>Since September 11, more than 1 million workers have exhausted their unemployment benefits, with 2 million more estimated to run out of benefits by this June.</p>
<p>In previous recessions, Congress acted to extend unemployment benefits beyond the 26-week limit. But this Congress has yet to extend benefits--holding workers hostage while they debate further tax cuts for the rich in Bush's "economic stimulus" package.</p>
<p>By every measure, poverty is on the rise, with millions of families reaching the point of desperation. Food aid organizations reported a 20 to 30 percent increase in people using food pantries nationwide since September 11. Nearly 1.4 million people filed for federal bankruptcy in 2001, an all-time high.</p>
<p>Roughly 2.2 million people lost their health insurance last year, mainly because of layoffs. And the more than 40 million uninsured doesn't even count the many millions of workers who are "underinsured"--whose health coverage doesn't cover their basic needs. The number of people who are either uninsured or underinsured has risen to 88 million--up from 60 million just two years ago.</p>
<p>Lacking adequate health insurance greatly increases the likelihood of experiencing other types of hardship. Families who lack health coverage are more than twice as likely to skip meals or forgo paying the rent, mortgage or utility bills.</p>
<p>And employment alone isn't a cure for poverty. Today, according to EPI economist Heather Boushey, "Nearly 37 million Americans go without some basic necessities, such as food, shelter, medical care. For one out of every three working families with young children, income alone is not enough to make ends meet."</p>
<p>Nearly half of all poor families include a full-time worker. Even before the recession started, 29 percent of workers were in jobs that pay poverty-level wages, a higher share than in the past.</p>
<p>Thus far, the Bush administration--concerned only with making life even better for the already rich--has failed to lift a finger to help those suffering hardship in this recession. Even the $300 federal tax rebate checks mailed out last summer--which seemed to be the one feature of Bush's tax cuts that would actually benefit ordinary people--has turned out to be a cruel hoax.</p>
<p>The "rebate" was not a rebate at all, merely an "advance" on this year's income taxes. Millions of people will realize this only when they reach line 47 while filling out their 1040 income tax form this year--and find that they now owe $300 more in taxes than they expected. Many will have to pay at least part of their "rebate" back to the government, in fact.</p>
<p>While the well-heeled are celebrating the end of the recession, the working class is still mired in it.</p>

