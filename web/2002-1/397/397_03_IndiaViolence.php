<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>What's behind the violence in India?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 8, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE MAINSTREAM media had a ready-made formula to explain why 58 Hindu train passengers were burned to death February 27 in the Indian state of Gujarat: Muslim fanaticism.</P>
<P>The formula was well worn after the September 11 attacks, the December 13 suicide attack on India's parliament, and the murder of <I>Wall Street Journal</I> reporter Daniel Pearl by Pakistani kidnappers.</P>
<P>But it didn't exactly fit the three days of nightmarish rioting by Hindus, in which nearly 10 times as many Muslims were killed.</P>
<P>India's ruling party, the Bharatiya Janata Party (BJP)--a pioneer of anti-Muslim scapegoating--claimed that an Islamist organization in arch-rival Pakistan was behind the initial attack and dismissed the killings of Muslims as unplanned communal violence. In other words, Muslims died in an outburst of anger as Hindus responded to a cold-blooded Muslim atrocity.</P>
<P>But the instigator of the anti-Muslim rioting was the Vishwa Hindu Parishad (VHP), a Hindu fundamentalist sister group of the BJP, which called a strike in order to fill the streets with angry Hindus.</P>
<P>In reality, the VHP created the climate in which the train attack took place. The train was packed with VHP members returning to Gujarat from a rally in the village of Ayodhya, where the group plans to build a Hindu temple on the ruins of a 16th-century Muslim mosque.</P>
<P>A VHP mob destroyed the mosque in 1992--on the pretext that it stood on the birthplace of the Hindu god Ram. Muslims, who face discrimination throughout India, see the VHP's temple campaign in the way that African Americans might view the construction of a Confederate memorial on the ashes of a burned Black church.</P>
<P>You wouldn't know it from the mainstream media's accounts, but most Hindus don't buy the VHP's Klan-like ideology. Attempts to spread the campaign of mass murder beyond Gujarat failed--leaving the number of Muslims killed lower than in the pogrom that followed the destruction of the Ayodhya mosque in 1992.</P>
<P>Just two weeks ago, voters rejected BJP candidates in every major state except Gujarat. Public revulsion could even force the BJP to block its sister group's attempt to begin building the temple on March 15.</P>
<P>The bloodshed in Gujarat is an outgrowth of the BJP's strategy of scapegoating Muslims--a ruthless approach to politics that makes these right-wingers trusted U.S. allies in the "war against terrorism."</P>

