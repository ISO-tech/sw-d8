<p><font face="Arial, Helvetica, sans-serif" size="2">March 8, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>ASAF ORON is one of 300 Israeli soldiers who has signed onto a petition stating that he won't serve in the Occupied Territories. Here, we reprint excerpts from Oron's statement.</P>
</B><P>ON FEBRUARY 5, 1985, I got up, left my home, went to the Compulsory Service Center on Rashi Street in Jerusalem&#133;Exactly 17 years later, I find myself in a head-to-head confrontation with the army, while the public at large is jeering and mocking me from the sidelines.</P>
<P>Right-wingers see me as a traitor who is dodging the holy war that's just around the corner. The political center shakes a finger at me self-righteously and lectures me about undermining democracy and politicizing the army. And the left? The square, establishment, "moderate" left that only yesterday was courting my vote now turns its back on me as well&#133;</P>
<P>Almost no one asks the main question--why would a regular guy get up one morning in the middle of life, work, the kids and decide he's not playing the game anymore?&#133;</P>
<P>You get used to [military service] in a hurry, and many even learn to like it. Where else can you go out on patrol--that is, walk the streets like a king, harass and humiliate pedestrians to your heart's content, and get into mischief with your buddies--and at the same time feel like a big hero defending your country?&#133;</P>
<P>Without thinking, I turned into the perfect occupation enforcer. I settled accounts with "upstarts" who didn't show enough respect. I tore up the personal documents of men my father's age. I hit, harassed, served as a bad example&#133;</P>
<P>The truth is that I understand why everyone is mad at us. We spoiled the neat little order of things.</P>

