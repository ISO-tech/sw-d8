<P><font face="Times New Roman, Times, serif" size="4">NYPD thugs go free! No more police brutality!</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Getting away with torture</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 8, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>IS THERE no limit to what the "justice" system will let New York police get away with?</P>
<P>Three cops who helped torture Haitian immigrant Abner Louima in 1997 are free. Last week, a federal appeals court threw out the convictions of Charles Schwarz, Thomas Wiese and Thomas Bruder for obstructing justice, and overturned Schwarz's conviction for holding down Louima while fellow officer Justin Volpe sodomized him.</P>
<P>Schwarz may be retried for participating in the torture. But Wiese and Bruder won't spend another minute in prison--though even the judges who freed them admit that they participated in one of the cruelest examples of police brutality in the long, sick history of the NYPD.</P>
<P>Cops from the 70th precinct in Brooklyn grabbed Louima off the street in August 1997 and brought him to the station house, where he was stripped from the waist down and dragged into a bathroom.</P>
<P>Two cops attacked him, shoving the wooden handle of a toilet plunger into his rectum, then into his mouth, smashing out four front teeth. He spent two months in the hospital recovering from severe injuries--and remains disabled today.</P>
<P>Volpe will still serve a 30-year prison sentence for torturing Louima--but only because he pled guilty. If he hadn't admitted his part in the assault, some well-paid lawyer for the Policemen's Benevolent Association might have found a technicality to get him freed--as they did for Wiese, Bruder and Schwarz.</P>
<P>"This ruling is a shock and an outrage," said Ray Laforest, a member of the Haitian Coalition for Justice who helped to organize protests in 1997 after Louima was brutalized. "What they did was torture."</P>
<P>Two weeks after Louima was brutalized, tens of thousands of people marched across the Brooklyn Bridge in protest. Even former Mayor Rudolph Giuliani--who defended every last crime committed by the NYPD--had to call for the torturers to be punished.</P>
<P>Likewise, waves of demonstrations and civil disobedience following the murder of Amadou Diallo--the unarmed Guinean immigrant who died in a hail of 41 cop bullets in 1999--cast a spotlight on the issues of police violence and racial profiling.</P>
<P>But after September 11, politicians and the media turned Giuliani and his cops into heroes. Now they think they can get away with anything--and that's the message the justice system sent last week.</P>
<P>We have to take a stand. "The only reason the cops came to trial at all was because Louima lived to tell his story--and because working people and people of color came out on the streets to demand justice," Laforest told <I>Socialist Worker</I>. "As long as this brutality continues, we must continue to make our voices heard."</P>

