<font face="Times New Roman, Times, serif" size="5"><b><P>New <I>Rollerball</I> packs bigger punch</P></b></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Leonard Klein</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 8, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>MOVIES: <I>Rollerball,</B></I> directed by John McTiernan, starring Chris Klein and LL Cool J.</P</font>
<font face="Times New Roman, Times, serif" size="3"><P>ALTHOUGH THE new movie <I>Rollerball</I> is based on the same sci-fi story as the 1975 movie of the same name, it comes to completely different conclusions.</P>
<P>The original movie is set in 2018, after the "corporate wars" have consolidated corporations' control over society. There are no nations, violence or wars, and no need for personal choice, because people "chose" stability over freedom.</P>
<P>In order to slake human nature's "need" for violence, the corporations invent rollerball, a deadly sport combining roller derby, jai alai and street brawling.</P>
<P>In a coincidence that won't be lost on followers of the Enron scandal, the 1975 <I>Rollerball</I> is set in Houston, where Jonathan plays for the Energy Corporation.</P>
<P>Jonathan is the leading scorer and holds a record for injuring the most players in a game. But the corporate heads want the star player to retire because "the game is created to demonstrate the futility of individual effort."</P>
<P>When Jonathan won't go, rule changes are instituted to make the game more violent in an attempt to kill him. The players go along with the changes, and in a fight to the death, Jonathan is the last man standing. The fans go wild.</P>
<P>This cynical view of human nature is turned on its head in the new <I>Rollerball</I>. This time, the action is centered in Kazakhstan in 2005--in a world of great inequality, where the ex-Soviet bureaucrats own the mines and factories.</P>
<P>In a conscious mirror of the factory team days of baseball and football, many of the players are miners during the day. Jonathan Cross (Chris Klein) and Marcus Ridley (LL Cool J) are two ringers from the U.S.</P>
<P>When new rules make the game more violent, Ridley notes, "It was only a matter of time until they figured out a little blood on the track would boost ratings."</P>
<P>Growing awareness of society's inequality drives Jonathan to upstage the bosses' show. In the final game, players are divided between those who oppose the escalating violence, staging a sit-down strike, and the bosses' stooges.</P>
<P>In the end, it's players' collective action and fan support that win the day.</P>

