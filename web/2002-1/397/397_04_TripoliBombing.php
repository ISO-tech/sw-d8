<P><font face="Times New Roman, Times, serif" size="4">Civilian casualties aren't simply statistics</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The toll of U.S. bombs</P></b></font>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 8, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Many years ago while I was living in Libya, I experienced firsthand the aerial bombardment of Tripoli. In the light of the recent tragic events in the U.S., Afghanistan and Palestine, I feel the need to speak up about my experiences in order to raise awareness about the horrors and unacceptable nature of violence.</P>
<P>On a crisp April night in 1986--when I was just six years old--I remember my parents, my sister and myself running out of our apartment door into the dark corridors of our building. We were rushing down the stairs and my feet were pierced by pieces of glass that came from the windows that had been shattered by the blast of American missiles that had landed in the neighborhood behind us.</P>
<P>Later on, as my father herded us onward into a building in the hospital complex where he worked, I saw a number of lights in the sky. I still do not know if they were real American fighter planes or whether they were anti-aircraft missiles fired by the Libyans, but they filled me with an acute and indescribable sense of terror.</P>
<P>I was spirited away to the relative safety of a small closed room where there were a number of other people. I remember my elder sister telling me that it was not the planes that were causing the horrific noises from outside, but flying objects that were meant to destroy any aircraft.</P>
<P>I spent several dreadful hours in that cold, dark, overcrowded room, screaming, crying and wailing.</P>
<P>The neighborhood that had been at the receiving end of the brunt of the American laser-guided bombs was a civilian neighborhood like so many others in Tripoli.</P>
<P>I remember my father taking our car out into the neighborhood right behind our building the day after, and I remember seeing destroyed homes and gutted buildings, collapsed roofs and windows that had been smashed in by the force of the explosions.</P>
<P>What I did not see was the true cost--the remains of the men, women and children, sleeping unpretentiously the night earlier, who were blown to pieces, possibly before even knowing what was going on.</P>
<P>Perhaps the international outcry and chorus of condemnation in the aftermath of the Tripoli bombing would have been greater if the reports from Tripoli had spoken of a building full of Eastern Europeans and Asians rather than a neighborhood full of Arab families.</P>
<P>The people who died--the majority of whom were Libyan civilians, men, women and children, in their sleep, whose only crime was to be living in the wrong part of town--are seldom if ever mentioned, and the attack itself was widely viewed as being just another measure in the "fight against terrorism."</P>
<P>It is precisely this lack of appreciation for what military actions mean for ordinary innocent people in places like Afghanistan, Yugoslavia, Palestine, Iraq and elsewhere that I find unacceptable.</P>
<P>I feel compelled by my conscience to say "enough." Civilians who are harmed during the course of a conflict must never be seen as "collateral damage" or, even worse, as statistics.</P>
<P>I feel I must encourage people to view the news not just in terms of headlines in ink and not just in terms of stories on TV and radio, but also in the context of people whose only crime is to be at the wrong place at the wrong time--and whose offence is no greater than that of children huddling in fear in the basements of hospitals.</P>
<B><P>Suhail Shafi,</B> Malta</P>

