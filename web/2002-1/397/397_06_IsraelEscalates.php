<P><font face="Times New Roman, Times, serif" size="5"><b>Israel escalates the war on Palestinians</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 8, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE MIDDLE East "peace" plan offered by Crown Prince Abdullah of Saudi Arabia made front-page news around the world last week. So the Israeli government was quick to respond--with an unprecedented ground offensive into Palestinian refugee camps that was guaranteed to produce more bloodshed.</P>
<P>Abdullah's proposal--under which Israel would withdraw from the Occupied Territories of Gaza and the West Bank in exchange for normalized relations with Arab countries--was hailed as a "breakthrough" that could end the 17-month-old Palestinian Intifada against Israeli occupation.</P>
<P>Even the U.S. government offered its backing, and Palestinian Authority (PA) chair Yasser Arafat pledged his full support.</P>
<P>Israeli Prime Minister Ariel Sharon had to say that he would discuss the deal. But he can never accept a proposal that requires the dismantling of Zionist settlements in the West Bank and Gaza.</P>
<P>So Sharon set out to torpedo the proposal--by staging his most violent assault on Palestinians since the Intifada began.</P>
<P>With guns blazing, Israel Defense Forces (IDF) troops invaded the Balata and Jenin refugee camps in the West Bank. At least 19 Palestinians were killed and hundreds injured.</P>
<P>Last weekend, Palestinian militants carried out a series of attacks and suicide bombings in retaliation--and Israel took the opportunity to escalate the slaughter even further.</P>
<P>The assault on the camps was an exercise in terror. After ordering residents in Balata to leave their homes, the IDF went from house to house, breaking down doors and walls. An IDF spokesperson later said that the "soldiers weren't destroying walls, but creating openings to get to the next house."</P>
<P>Jibril Rajoub, the Palestinian security chief in the West Bank, called the raids "a predetermined massacre by the occupation authorities aimed at killing any effort at calm." Yet this escalation is the latest stage in the Israeli government's long war on Palestinians.</P>

