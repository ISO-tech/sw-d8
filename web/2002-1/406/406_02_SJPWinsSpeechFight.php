<P><font face="Times New Roman, Times, serif" size="4">Suspension of Palestinian rights group lifted</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Free speech fight wins at Berkeley</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 10, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>A PRO-PALESTINIAN student group at the University of California-Berkeley won its free speech fight against the administration's attempts to silence it. After an outpouring of support--both on campus and around the country--representatives of Students for Justice in Palestine (SJP) were told by university officials on Monday that the group's suspension had been lifted.</P>
<P>The stakes in this struggle were high. If administrators had succeeded in banning one of the strongest Palestinian rights groups in the country, the implications would have been ominous for campus activism everywhere.</P>
<P>The SJP was suspended last month after an April 9 demonstration of 1,200 people, which included a nonviolent sit-in that resulted in 79 arrests. The suspension barred the SJP from organizing events or meetings on campus, distributing leaflets or setting up information tables.</P>
<P>But SJP members defied this attack and set up tables last week. They weren't alone, either. Other student groups tabled with them--and in an inspiring show of solidarity, displayed signs that read, "We are all Students for Justice in Palestine." Days later, the SJP held a "Free speech and free Palestine" rally that marched to Chancellor Robert Berdahl's office.</P>
<P>The SJP's victory "would not have been possible without the outpouring of support that we received from all over the country and the support that we received from members of the campus community and faculty," said Snehal Shingavi, a member of the SJP and the International Socialist Organization.</P>
<P>But while the SJP's suspension has been lifted, the university is pressing ahead with criminal charges stemming from the sit-in. For protesters who get arrested on campus, criminal charges from the city of Berkeley are usually dropped within a couple of weeks. Berkeley probably hasn't convicted a student protester in three decades.</P>
<P>But the university is bent on seeing protesters prosecuted on charges ranging from trespassing to preventing a public employee from doing his or her job to resisting arrest. One student was charged with assault and battery against a police officer. Dates for hearings on the charges will be set this week, and the SJP is holding actions to let Berkeley officials know that their targeting of free speech won't go unnoticed.</P>
<P>"There's a real recognition that we're standing in the legacy of the Free Speech Movement (FSM), which Berkeley students organized in the 1960s," said Shingavi. "Three years ago, the university opened up the FSM Caf&eacute; to commemorate free speech in the abstract, but this isn't a real right. Identical things happened to the SJP today. And that's forcing people to recognize that the university isn't neutral in the political debates on campus--and that today, nearly 40 years later, we're still fighting for free speech."</P>

