<P><font face="Times New Roman, Times, serif" size="5"><b>Defiance in the streets</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 10, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>AS MANY as 1 million people jammed the streets of Paris on May Day as the traditional workers' day protest was transformed into a massive anti-Le Pen demonstration. A combined half million more people marched in smaller cities.</P>
<P>The Paris march, led by the unions, was scheduled to step off at 3 p.m. But four hours later, many contingents were still at the starting point. Everywhere you looked, there was a sea of humanity, filled with defiance. Emotions were so intense that complete strangers hugged one another and cried tears of joy. Union sound trucks weren't powerful enough to spread chants throughout the whole crowd, but people carried on anyway with their own chants.</P>
<P>High school students--on strike across the country since the first round of the election on April 21--revved up people by chanting "Motiv&eacute;, motiv&eacute;" ("We're motivated"). Many carried homemade placards of the front page from the April 22 issue of the newspaper <I>Liberation</I>--with a picture of Le Pen and a huge "Non."</P>
<P>The CGT union federation had several banners held by Black workers that read, "Daughters and sons of Black slaves against fascism and racism." The most popular chants were "F for fascist: N for Nazi," and "We are all the children of immigrants."</P>
<P>Gaby, an immigrant from the Ivory Coast, is a member of the left-wing CGT union federation and a janitor in a Paris hospital. "We must remember what happened in the '30s," Gaby said. "First Hitler came for the Jews and then the rest. My friends will only be his first victims. He has horrible plans for the rest of France."</P>
<P>Thomas, an unemployed retail worker, agreed. "Workers who voted for Le Pen are disgusted with their standard of living," he said. "But they are without hope. I look at these kids in the streets, the unions, the spirit, and I have hope that my life will improve. I say down with Le Pen."</P>
<P>Many on the protest said that while they were planning to vote for Chirac, they would do so reluctantly. "Vote for the crook, not the Nazi," was a popular slogan--referring to the numerous scandals Chirac is embroiled in.</P>
<P>But some people planned to abstain or cast a blank vote. "For the first time ever, I'm going to turn in a blank vote," said Remy, a 54-year-old office worker. "Maybe I'll write 'shit' on it, because that's the way I feel right now."</P>
<P>Mariane, a member of the CGT teachers' union, said, "I am a leftist, so I cannot vote for Chirac. I have a memory that goes beyond April 21. [Chirac] is also an enemy of workers."</P>
<P>In fact, the May Day demonstration wasn't just a showing of opposition to Le Pen, but a display of working-class power and defiance. One veteran socialist called it a "mini-May 1968," a reference to the great general strike of that year.</P>
<P>Even mainstream political analyst Emmanuel Todd saw the election as a sign of "the rebirth of class conflict." "It's like an iceberg coming out of the sea," he told reporters.</P>
<P>Danielle Sallandre, head of the CGT local at the Ministry of Employment, Health and Security, said, "After the election, we have to fight in the factories and offices to pressure Chirac. Monday, we're planning the first meeting of Ras l'Front (Enough of the Front). It's the first step in bringing political action to my workplace. But what we really need is a new, unified, anticapitalist party."</P>
<P>The May Day protests and the outpouring of antifascist feeling showed the potential to do just that--and to build a movement that can transform society.</P>

