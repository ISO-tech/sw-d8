<P><font face="Times New Roman, Times, serif" size="5"><b>Zionism doesn't challenge anti-Semitism</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 10, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>In answer to Yves Coleman's letter (<I>SW,</I> April 19): Zionism is not Judaism. It seems that this cannot be said enough.</P>
<P>Judaism is a religion. Zionism is an imperialist ideology. Judaism is thousands of years old. Zionism was born at the end of the 19th century, and even at that time, the goal of a Jewish-only state wasn't exclusively focused on Palestine; Uganda was considered, then discarded.</P>
<P>In insisting that Judaism and Zionism are one and the same, Coleman does not serve the interests of Jews, but rather the Israeli and U.S. states. Equating Zionism with Judaism, anti-Zionism with anti-Semitism, provides a cover for imperialist atrocities that have absolutely nothing to do with the Jewish faith.</P>
<P>A growing number of Jews both inside and outside Israel oppose the Israeli occupation and have criticized it quite harshly. Are these Jews anti-Semitic? Or are they outraged that human rights are being trampled in their names?</P>
<P>Coleman rightly shows concern about rising anti-Semitism in Europe. We as socialists must fight against anti-Semitism in every instance. The hundreds of thousands who poured into the streets to protest the rise of fascist Jean-Marie Le Pen showed how to do this best.</P>
<P>But the formation of Israel has never had anything to do with fighting anti-Semitism. Rather, it was a retreat from this fight, which has succeeded only in serving larger imperialist powers--first Great Britain and now the United States.</P>
<P>Yes, we must fight anti-Semitism. But defending Israel isn't the way. Zionism is not Judaism.</P>
<B><P>Elizabeth Terzakis,</B> Oakland, Calif.</P>

