<p><a name="Jospin"></a><font face="Times New Roman, Times, serif" size="4"><b>Don't blame Jospin for Le Pen</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Your analysis of the recent first round of French elections (<I>SW,</I> April 26) oversimplifies the record of Prime Minister Lionel Jospin.</P>
<P>With 20/20 hindsight, you look at the results of the election and cite disaffection with the mainstream parties to explain the success of the far right and far left at the polls. In explaining this disaffection, you say, "Jospin mainly pursued the same agenda as his conservative predecessors, privatizing more than the previous five governments combined."</P>
<P>This paints a one-sided picture, making Jospin look like a Bill Clinton figure who sold out the constituency of the party as "the lesser of two evils." While Jospin did carry out privatizations, he passed progressive legislation as well. Laws mandating a 35-hour workweek, granting gays and lesbians domestic partnership rights and guaranteeing social security for all are examples of this. Moreover, while the unemployment rate in France remains high, it has decreased in Jospin's five years.</P>
<P>If disaffection with mainstream French politics explains the success of French fascist candidate Jean-Marie Le Pen, then this disaffection goes much farther back in time than the Jospin government. While Le Pen received 17 percent of the vote in this year's election, he also received 15 percent of the vote in 1995 and 14 percent in 1988. In addition, the lower voter turnout this year means Le Pen got about as many votes in past elections as in this one.</P>
<P>Your sole emphasis on the policies of the current government de-emphasizes the intractable nature of Le Pen's popularity. In doing so, you unwittingly underplay the threat his candidacy poses to the future of French politics.</P>
<B><P>Julien Ball,</B> Chicago</P>

