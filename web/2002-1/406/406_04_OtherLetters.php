<p><a name="LA"></a><font face="Times New Roman, Times, serif" size="4"><b>The uprising in LA wasn't progressive</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Your recent article on the Los Angeles uprising in 1992 (<I>SW,</I> April 26) defies logic. To classify it as an inspiring example of a progressive, workers rebellion is to close one's eyes to the reality of the events.</P>
<P>While it is true the left was on hand to organize a few small protests, the riots represent an overall failure of the left to direct the mass anger towards the real enemy.</P>
<P>You mentioned the "immaturity" of some aspects of the rebellion, but the brutal assaults on innocent people displayed more of a reactionary character. And Korean shop owners weren't the only ones to suffer from reactionary, racist violence. There were dozens of attacks on non-Black working class people in Los Angeles alone. These types of atrocities were repeated in cities across the nation.</P>
<P>While you may have been inspired by these events, revolutionary workers were horrified at the lack of class consciousness and basic decency of so many. This insipid paean to blind rage fools no one.</P>
<B><P>Jim Tapscott,</B> Albany, Ga.</P>

