<p><a name="Conquer"></a><font face="Times New Roman, Times, serif" size="4"><b>They want to divide and conquer</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Your article on the Institute of Medicine's health care study (<I>SW,</I> April 5) let me down. Finally, a report came out that highlights "class" as a far more determinative factor than "race." The class-conscious among us should immediately pick this up and run with it.</P>
<P>The idea of the "minority struggle" is a trick. Poor whites start thinking that they're "middle class"--in spite of going deeper in debt every year and never making ends meet.</P>
<P>Racial identities in a class struggle will always be watered down by the wealthiest of each group. After all, everyone could name a host of minority millionaires who have access to the big microphones.</P>
<P>There can be no realistic oppressed minority struggle (we must be specific lest we accidentally claim Clarence Thomas, Colin Powell or Condoleezza Rice) without the strength of the oppressed majority.</P>
<P>So please, be genuine socialists and resist the Good Cop/Bad Cop mind game of racial division in America. The best and most useful triumph over racism would be a unity of everyone who is committed to the ideals of justice and equality.</P>
<B><P>Bruce Reilly,</B> Cranston, R.I.</P>

