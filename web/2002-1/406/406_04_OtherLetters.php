<P><font face="Times New Roman, Times, serif" size="4"><b>Challenging Israel's lies here at home</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>I'm a student at the University of Iowa and a member of the International Socialist Organization. Recently, we heard that there was to be a pro-Israel rally in Iowa City. We sent out e-mails and made phone calls, organizing a counterdemonstration.</P>
<P>The following day, about 60 people showed up for the pro-Israel rally--and about 60 people showed up for our counterdemonstration!</P>
<P>The speakers at the rally were saying that Israel has constantly shown "restraint." We were there to tell people that Israel has shown anything but restraint. From the occupation of Ramallah to the massacre in Jenin, Israel has proven itself an apartheid state. One cannot equate the missiles and machine guns of the Israeli war machine to the desperate measures of Palestinian suicide bombers.</P>
<P>We were encouraged that so many people showed up on such short notice. We can't let Palestinians fight this battle alone. It is important that we fight back here in the U.S., where much of our money goes to fund the Israeli military.</P>
<B><P>Mitch Day,</B> Iowa City, Iowa</P>

