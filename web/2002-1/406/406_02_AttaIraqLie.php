<P><font face="Times New Roman, Times, serif" size="5"><b>The Iraq connection that never was</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 10, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ANOTHER OF the U.S. government's post-September 11 lies has been exposed--and from the horse's mouth. According to <I>Newsweek</I> magazine, U.S. officials have confirmed that the alleged leader of the September 11 hijackers, Mohamed Atta, couldn't have met with an Iraqi intelligence agent in Prague five months before.</P>
<P>From within hours of the September 11 attacks, Washington's politicians and pundits began searching for a smoking gun to implicate Iraq--and serve as an excuse for another U.S. war. Atta's mythical meeting in the Czech Republic was just the ticket.</P>
<P>Countless commentators repeated the rumor that Atta traveled to Prague in April 2001 to meet with Ahmed Khalil Ibrahim Samir al-Ani, an Iraqi diplomat and Saddam's roving super-spy, according to the accounts.</P>
<P>Depending on which blowhard you listened to, al-Ani was either delivering Saddam's personal orders for the September 11 attacks--or arranging for the delivery of anthrax to an unspecified al-Qaeda cell for a follow-up mail attack.</P>
<P>Finally, eight months after September 11, the FBI and Czech authorities have settled on the same story. The Czechs--who went back and forth on their story numerous times--now say they have no solid evidence that Atta was in Prague, and U.S. officials admit there's no evidence that he left the U.S.</P>

