<P><font face="Times New Roman, Times, serif" size="4">NYC students fight cutbacks:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Education is a right!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Vivian Silverio and Lee Wengraf</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 10, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"1-2-3-4, CUNY needs an open door! 5-6-7-8, don't expel, educate!" That was the chant that rang out in New York April 30 as nearly 1,000 high school and City University of New York (CUNY) students protested outside City Hall.</P>
<P>The demonstrators turned out to show their anger at city and state plans to cut $358 million from the public education budget and $155 million from the Tuition Assistance Program (TAP) for college students.</P>
<P>New York high schools face sharply reduced funding for classes, activities and staff. This attack sparked a walkout of several hundred high school students on April 30. The students started their demonstration in Madison Square Park, then marched to Union Square to join the rally organized by the CUNY for All Coalition.</P>
<P>"We're here because we've had cuts to a lot of programs, and our public schools are the worst in the country," a student from DeWitt Clinton High School told <I>Socialist Worker</I>. At the rally, CUNY students spoke about how the citywide university system represents the one opportunity for poor and working-class New Yorkers to get a college education.</P>
<P>The past several months have seen a revived effort to mobilize against cuts at CUNY--provoked by Gov. George Pataki's doubling of tuition for undocumented immigrant students and other attacks. "I've been here four years and knew that people fighting the cuts existed, but now, I'm getting involved," said Melissa, a Hunter College student who came out with her Community Organizing class.</P>
<P>Although the protest was spirited, the attendance of CUNY students was smaller than organizers had hoped. "The turnout of a lot of high school students here is important," said Ed Ott, director of public policy for the Central Labor Council. "But it should have been bigger. There's been a decade and a half of cuts on the backs of working-class youth. That's a class crime, especially with tax cuts to the wealthy."</P>
<P>The next step will be to build on the organizing so far. We need more anti-cuts committees on more campuses to draw as many students as possible into the fight. And we also need more solidarity from CUNY professors.</P>
<P>As Paulette from Hunter College put it, "It's not enough to just have a presence. We have to get more people involved."</P>

