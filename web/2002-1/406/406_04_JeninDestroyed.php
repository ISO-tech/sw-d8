<P><font face="Times New Roman, Times, serif" size="4">"If there is a hell on earth, it is here in Jenin"</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Destruction beyond words</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 10, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>I'm writing from East Jerusalem. I paid a driver $150 just to drive me here from Jenin so that I could take a shower. It was too much to withstand--what I saw and the smells that saturated my body, my hair.</P>
<P>If there is a hell on earth, it is here in Jenin. The destruction is beyond words. If you see it up close and personal, no word other than "massacre" can describe what happened there. No home was spared. We found a decomposed body yesterday beneath the rubble of a home.</P>
<P>Three people exploded as they were trying to salvage their belongings. Apparently, the Israel Defense Forces (IDF) left them "surprises."</P>
<P>We stayed with a family in the camp. Their story was heartbreaking, but they fared better than most. Their home, although pitted with gunshots and without doors or windows, was inhabitable.</P>
<P>The camp is still without water, and everyone stinks. Couple that with rotting corpses, and you might get the picture.</P>
<P>Our host family was cornered in half a room for three days. They had to relieve themselves in one pot. IDF soldiers, meanwhile, defecated all over their home--on their beds, in their cooking pots, on their bread, and also on their copy of the Koran.</P>
<P>One woman with an asthmatic son watched her husband get stripped and then shot by the IDF. Later, he was run over by a tank. She found one of his shoes this morning, on her way to look for her son's medicine in the rubble of her demolished home.</P>
<P>If I wasn't looking at the devastation and destruction with my own eyes, I might have thought that the stories are too horrific to be real. What you see on television does not begin to tell the story.</P>
<P>But I also think the spirit of the people there is perhaps the most enduring, tough, steadfast and charitable that I have ever seen in all my life. Imagine this: As I was leaving, one of my new friends there, a young man named Ahmed, about 15, came over to me with his mother and a small bag of falafel--apologizing that he didn't have enough money to bring me more for breakfast before I left!</P>
<P>There were no terrorists in that camp. Those who died resisted to their last breath. And those who remain are the most resilient and warm people I've known.</P>
<P>I am still trying to get into Ramallah. I've been turned away at Qalandia twice--once with a gun pointing at my face when a journalist and I tried to sneak around the Mahsoom checkpoint. I'm told tomorrow the curfew might be lifted, so hopefully I'll get in for a while.</P>
<B><P>Susan Abulhawa,</B> Jerusalem</P>

