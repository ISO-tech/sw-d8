<P><font face="Times New Roman, Times, serif" size="5"><b>University of Massachusetts</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Doug Murray</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 10, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ADMINISTRATORS AT the University of Massachusetts at Amherst have gone on the offensive against a union for resident assistants (RAs). The six undergraduates who were among 35 people arrested at a campus sit-in last week were suspended from school. This will cost them their jobs, housing and course credits for the semester--and several won't graduate.</P>
<P>"It's outrageous that UMass is punishing people for standing up for their right to unionize," said Melissa Burke, a UMass RA. "Pressure has to be put on the administration not just for the recognition of the union, but for students to have the basic right to dissent about issues that affect them."</P>
<P>The suspensions stem from an April 29 sit-in at the Whitmore Administration Building to protest the university's refusal to recognize the first undergraduate union in the country to win a certification election. Some 15 graduate and undergraduate students occupied the Dean of Student Affairs office while 50 protesters gathered outside in solidarity.</P>
<P>Then the cops moved in, arresting 35 people, who were charged with disorderly conduct and resisting arrest. Sit-in participants were also charged with trespassing. Police in riot gear pulled students from a picket line outside as they attempted to block a bus from taking away the arrested protesters.</P>
<P>The sit-in was called by United Auto Workers Local 2322, after administrators stonewalled negotiations with the newly organized RAs. While phone calls and letters have put some pressure on the administration, more is needed to overturn the suspension of the activists and win union recognition.</P>
<P>We have to spread the struggle outside the university--by uniting the RAs' union campaign to the statewide fight against budget cuts.</P>

