<P><font face="Times New Roman, Times, serif" size="5"><b>White House retreats from student loan scam</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 10, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH administration was forced to retreat last week from a scheme to steal money from a federal student loan program to pay for tax cuts.</P>
<P>White House Budget Director Mitch Daniels floated the proposal during talks with members of Congress over what to do about a federal government deficit that could balloon past $100 billion this year--thanks most of all to George W. Bush's $1.3 trillion tax giveaway to the rich last year.</P>
<P>Daniels suggested eliminating a program used by millions of college students and graduates to consolidate education loans at a fixed, federally subsidized interest rate. Offering the consolidation loans at variable rates would make them less appealing, Daniels said--and save $1.3 billion.</P>
<P>Congressional Democrats exposed the scheme, embarrassing the White House into withdrawing it. But the controversy cast a spotlight on a deeper problem--the growing gap between soaring tuition costs and lagging increases in financial aid programs.</P>
<P>According to a study released last week by the National Center for Public Policy and Higher Education, "Only the wealthiest families have seen their incomes keep pace with increases in tuition. The lowest-income families have lost the most ground, and this is a major factor in their lower rates of college attendance."</P>
<P>The Bush gang may have retreated from its most recent proposal. But Washington will keep cutting away at programs that give working people access to higher education--because the priority for politicians is helping out their rich pals.</P>

