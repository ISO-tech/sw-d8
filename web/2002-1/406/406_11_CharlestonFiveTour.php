<P><font face="Times New Roman, Times, serif" size="4">Charleston Five kick off their victory tour in Chicago</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Celebration and solidarity</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 10, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--More than 200 people packed the UNITE union hall here last week to celebrate the victory of the Charleston Five and May Day--and to pledge their solidarity to workers in struggle.</P>
<P>The five--Ricky Simmons, Peter Washington, Jason Edgerton, Kenneth Jefferson and Elijah Ford--are members of the International Longshoremen's Association (ILA) who spent 18 months under house arrest after police attacked their picket line. An international solidarity campaign won freedom for the five last fall.</P>
<P>The first stop for the five in Chicago was a picket line by UNITE members on strike for their first contract at Carousel Linens. "This is a great day for labor here in Chicago because we are in the presence of heroes," UNITE organizer Pete DeMay said at the May 2 evening rally.</P>
<P>"In the wake of 9/11, corporate media have tried to tell us who our heroes are. They say our heroes are George Bush, our heroes are the people who run the Microsofts and Enrons of the world, because they worked hard and made it big. And they say how they're going to go out and fight terrorism. But I think we all know that they're the biggest terrorists, they're the ones terrorizing workers every day."</P>
<P>The event brought together members of several different unions, including members of the UFCW, Teamsters, OPEIU, AFSCME, the Chicago Teachers Union and the Graduate Employees Organization.</P>
<P>Speakers included IBEW Local 134 President Mike Fitzgerald, AFT Local 1708 President Dolores Withers, SEIU Local 73 Vice President Cynthia Rodriguez and Erica Hade, field secretary of the Illinois AFL-CIO.</P>
<P>Tim Leahy, assistant to the president of the Chicago Federation of Labor, spoke of the connection between the Charleston Five and the Haymarket martyrs, executed for their role in 1886 fight for the eight-hour day in the first May Day. "I know that as I stand here [the Haymarket Martyrs] would be so proud of you, the Charleston Five," he said. "The same solidarity that pushed the Charleston Five to victory will push the Carousel workers to victory."</P>
<P>Rev. Calvin Morris, executive director of the Community Renewal Society and religious co-chair of Jobs with Justice, brought the audience to its feet. "The Charleston Five represents for us the kind of outcome that can occur when groups of people work together, stand together, struggle together, march together, give out of their means together--even in this rough period in American history."</P>
<P>The Carousel workers took the stage as the crowd chanted "S&iacute; se puede" ("Yes we can"). "We decided to fight the fight that we're waging now in case members of our family want to come to this country later so that they don't have to go through what we've gone through," said striker Miriam Perez.</P>
<P>Lee Sustar of the International Socialist Organization spoke of how the firings of immigrant workers in airports are being used to further the employers' anti-labor agenda. "They're using that as a wedge to attack all our unions," he said. "Just as they say that those of us who oppose the war in Afghanistan or support justice for Palestine are on the side of terrorists."</P>
<P>After the five introduced themselves and expressed their thanks, Leonard Riley, an activist in ILA Local 1422 and a key organizer of the defense campaign, gave the final speech. Comparing the state of the labor movement with a sporting event, Riley said that while our side was behind in the fourth quarter, "We can come back to win."</P>
<P>Later that evening, Riley and the five addressed the membership meeting of IBEW Local 134. The following day, the five visited Haymarket monument in Waldheim Cemetery, where Leonard Riley made an impromptu May Day speech along with Hignicio Barrios of Mexico's independent FAT labor federation.</P>
<P>The five were also recognized at the weekly meeting of Rev. Jesse Jackson's Rainbow/PUSH Coalition and appeared before the membership meeting of SEIU Local 73 and the annual banquet of the Chicago chapter of the Labor Party.</P>
<P>The five then traveled to Detroit for meetings sponsored by United Auto Workers Local 22 and <I>Labor Notes</I> magazine.</P>
