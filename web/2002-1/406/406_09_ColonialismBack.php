<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Why is colonialism respectable again?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 10, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ONE OF the worst developments in the post-September 11 battle of ideas is the newfound respectability of colonialism.</P>
<P>"Afghanistan and other troubled lands today cry out for the sort of enlightened foreign administration once provided by self-confident Englishmen in jodhpurs and pith helmets," Max Boot wrote in the <I>Weekly Standard</I> a week after Bush's war in Afghanistan began last October.</P>
<I><P>National Review</I> editor Richard Lowry called for establishing a U.S.-sponsored "protectorate" over Iraq after U.S. troops oust Saddam Hussein's regime.</P>
<P>The Bush administration has made no secret of its desire to overthrow the Iraqi government--and, possibly, other governments as well. U.S. troops are already occupying Afghanistan, providing a bodyguard for the puppet government they installed.</P>
<P>But as Western rulers discuss a "new imperialism" and a "return to colonialism," it's worth considering just what they propose to revive.</P>
<P>A century ago, Europeans controlled 90 percent of African territory and almost all of Polynesia.</P>
<P>The U.S. got into the act, making off with the Philippines, Guam, Cuba and Puerto Rico after the 1898 Spanish-American war. It still maintains a colony in Puerto Rico today.</P>
<P>Today's new imperialists accept at face value the claims of European colonialists that they were motivated by a mission to "uplift the backward races"--what imperialist poet Rudyard Kipling called "the white man's burden."</P>
<P>But colonialism was anything but benign. In the last quarter of the 19th century, as many as 61 million people perished from famines in India, China and Brazil whose root causes lay not in weather patterns, but in the colonial reengineering of their societies.</P>
<P>The 1898-1902 U.S. war to subjugate the Philippines slaughtered more than 1 million people. U.S. forces fought Filipino guerrillas and employed all the techniques of "pacification" later used in Vietnam: concentration camps, crop destruction, scorched earth and biological warfare.</P>
<P>Today's conservative colonialists are following in the footsteps of liberals who claimed that the West had a "duty" to intervene to prevent humanitarian disasters in a number of countries in the 1990s--from Somalia to Haiti to the Balkans.</P>
<P>Of course, the liberal champions of humanitarian intervention don't call what they advocate "colonialism." But NATO troops occupy Kosovo and Bosnia, supporting UN-appointed governments empowered to decide everything from local candidates to school textbooks.</P>
<P>A similar operation is winding up in East Timor. U.S. envoys in there are working out deals for Western exploitation of oil deposits off the coast of the newly independent country. Meanwhile, more than half of Timorese are unemployed, and the UN hasn't even tried to assure the population's access to drinking water.</P>
<P>These operations have little to do with humanitarianism and everything to do with preserving U.S. power in these strategic areas. A century ago, Mark Twain exposed the lies behind U.S. imperial conquests. "[I used to be] a red-hot imperialist," he wrote. "I wanted the American eagle to go screaming into the Pacific&#133;Why not spread its wings over the Philippines&#133;We can make [the Filipinos] as free as ourselves, give them a government&#133;start a brand new republic to take its place among the free nations of the world."</P>
<P>"But I have thought some more since then&#133;and I have seen that we do not intend to free, but to subjugate the people of the Philippines. We have gone there to conquer, not to redeem&#133;And so I am an anti-imperialist. I am opposed to having the eagle put its talons on any other land."</P>
<P>The best argument against colonialism of all came from the billions of Indians, Vietnamese, Algerians and others who fought to drive colonizers out of their countries. Anyone committed to democracy, equality and freedom should celebrate the defeat of colonialism--not hanker for its revival.</P>

