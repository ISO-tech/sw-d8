<P><font face="Times New Roman, Times, serif" size="5"><b>LA school bus drivers settle</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By David Rapkin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 10, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LOS ANGELES--School bus drivers here voted in late April to end their 26-day walkout and accept the contract offered by Laidlaw Education Services. Drivers--members of Teamsters Local 572--voted 339 to 58 to return to work on the recommendation of the union leadership.</P>
<P>Unfortunately, the three-year contract that drivers accepted differs little from the company's offer before the strike a month ago. "We fought hard and got almost nothing for it," Kristina, a veteran driver, told <I>Socialist Worker</I>.</P>
<P>The settlement includes a 3.5 percent raise each year and a new week of vacation. But drivers were asking for a 5 percent raise, which still would have left them making far less than their counterparts who work directly for the school district.</P>
<P>"Even the 15 percent [over three years] is nothing but a slap in the face, considering that we take thousands of kids everywhere in LA traffic, safely, day after day," said driver Jason Baldwin.</P>
<P>In addition to the wage settlement, drivers accepted the same terribly inadequate pension plan they struck to change. Health care, a central issue, will go to arbitration.</P>
<P>Even many drivers who grumbled about the contract voted for it. Although the drivers gained little in wages and benefits on the picket line, many workers felt that striking had put them on firmer footing.</P>

