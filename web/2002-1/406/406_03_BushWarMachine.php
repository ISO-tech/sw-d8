<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Bush's new excuse for a dirty war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 10, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"JUST AS we fight terrorism in the mountains of South Asia, we will fight terrorism in our own hemisphere." That's what Attorney General John Ashcroft declared last week as he announced the indictment of six members of a left-wing rebel group in Colombia for the 1999 murder of three Americans.</P>
<P>The indictment is a calculated move by the Bush administration to pitch their "war on terrorism" for the Western Hemisphere--by once again targeting Colombia's left-wing guerrillas.</P>
<P>Of course, Ashcroft failed to mention that the U.S. government's allies are the real terrorists. According to Amnesty International, right-wing paramilitary groups with direct ties to the Colombian military are responsible for the vast majority of the country's political violence.</P>
<P>Yet Secretary of State Colin Powell declared last week that Colombia's armed forces have met human rights criteria--so the U.S. will release more than $60 million in military aid to "fight terrorism," with another $40 million to come later in the year.</P>
<P>Even as it was ensuring that more human rights violations take place in Colombia, the Bush administration was protecting its own killers--by withdrawing from a treaty this week that would have made the U.S. subject to an international criminal court.</P>
<P>Why did the U.S. withdraw? Because the Bush gang doesn't want the U.S. government's own dirty record to come under any scrutiny.</P>
<P>And for good reason. The reality of the U.S. war on terrorism has become even clearer in Afghanistan--where thousands of people were killed by U.S. bombs, and now those who survived have to endure the tyranny of the country's new "rulers," handpicked by the U.S. Like Northern Alliance warlord Gen. Abdurrashid Dostum, who is now officially in charge of the Shebergan prison near Mazar-i-Sharif, where more than 2,700 prisoners--mostly young, illiterate men conscripted by the Taliban--are being held.</P>
<P>Conditions in the prison are so bad that in April, the International Red Cross began emergency liquid feedings of more than 100 prisoners. Many are expected to die. "In a way," wrote the <I>Washington Post,</I> "they will be the lucky ones: the rest of the detainees at Shebergan will go on living 50 to a room, without toilets, clean water, or sanitary measures."</P>
<P>The U.S. is responsible for this barbarism--even as they prepare to carry out a new slaughter in Iraq. "The people over at the Pentagon strongly believe not only that we should [invade Iraq], but that we will," one State Department official recently told the <I>Boston Globe</I>. "In their minds, it's a matter of putting something in front of the president and not having him decide whether we will do it, but when."</P>
<P>It's time to step up the struggle. Last month's big antiwar demonstration in Washington, D.C., shows the potential to build a movement to stop Bush's war machine.</P>

