<P><font face="Times New Roman, Times, serif" size="5"><b>Defeating the Nazi menace</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 10, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE RISE of Jean-Marie Le Pen is part of the growth of the far right across Western Europe. In Italy, a "reformed" fascist party runs government ministries. In Denmark, Austria and Portugal, anti-immigrant parties are in the ruling coalition. And in Holland, anti-immigrant Pim Fortuyn was expected to take 20 percent of the vote before he was assassinated May 6.</P>
<P>Most of these parties made headway by tapping into discontent with the center-left governments that came to office in much of Western Europe in the late 1990s. Parties ranging from the Democratic Left (former Communists) in Italy to the British Labour Party adopted anti-union, free-market policies modeled on Bill Clinton's Democrats in the U.S.</P>
<P>Instead of confronting the far right's policies, these governments have often tried to outflank them. For example, a British government minister in Tony Blair's Labour Party responded to Le Pen's electoral success by calling for stricter immigration controls. His logic? Allowing refugees to enter the country would hand a "firelighter" to the neo-Nazi British National Party (BNP).</P>
<P>In fact, anti-immigrant policies have only legitimized the BNP, which captured three seats in a municipal council election recently.</P>
<P>In France, Jospin's rhetoric was more left wing than Blair's--but his policies were not so different. Le Pen benefited from this in the first round of the presidential elections by winning the votes of 30 percent of manual workers, compared to 12 percent for Jospin.</P>
<P>In the working-class stronghold of Nord-Pas de Calais, Le Pen won more than 19 percent of the vote in the first round--the highest total in a district that used to vote Communist. Some 38 percent of the unemployed voted for Le Pen in the first round.</P>
<P>Le Pen's appeals to the long-term jobless are modeled on the rise of the Nazis in Germany in the 1930s. By mobilizing those left out by the system, he hopes to turn the National Front into a mass, counterrevolutionary movement that can someday seize power, smash the unions and abolish democracy, just as Hitler did.</P>
<P>Such a movement doesn't yet exist. Le Pen was able to muster just 20,000 to his May Day protest in Paris. But Le Pen still won the support of one voter in five. That's why it's important to turn the tremendous anti-Le Pen demonstrations into an antifascist movement that confronts the Nazis wherever they show their faces.</P>
<P>At the same time, the movement must take on the political and economic issues that Le Pen feeds on--fighting for immigrant rights, jobs and social programs to overcome the poverty that grips millions.</P>
<P>Such a movement must be anchored by a revolutionary party rooted in the working class--one that can provide a positive alternative to a discredited system and offer a vision of hope, liberation and workers' power against the barbaric future of Le Pen.</P>
<P>The outpouring of opposition to Le Pen--coming in the wake of big strike waves in recent years, plus the strong showing of the revolutionary left in the first round--show the possibility of building such a party.</P>

