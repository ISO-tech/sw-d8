<P><font face="Times New Roman, Times, serif" size="4">Bush and Sharon</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Partners in war crimes</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 10, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE BULLDOZER is in town--and Washington has rolled out the red carpet.</P>
<P>Israeli Prime Minister Ariel Sharon earned this nickname earlier in his war crimes career with his enthusiasm for demolishing Palestinian homes to clear the way for Israeli settlements.</P>
<P>But since his last visit to Washington, D.C., a few months ago, Sharon has a new claim to fame--as the butcher of Jenin. "It is totally destroyed," said United Nations Middle East envoy Terje Roed-Larsen in mid-April, after seeing the devastation left behind after Israel's scorched-earth assault on the Jenin refugee camp in the West Bank.</P>
<P>People around the world were outraged by the hellish pictures of the destruction in Jenin--of the survivors scratching at the rubble in search for any remnant of the life that Israeli soldiers took from them.</P>
<P>Around the world, but not in Washington, D.C. As Sharon was packing for his visit, the U.S. Congress pledged its full support for his government in nonbinding resolutions, passed by lopsided margins in both houses.</P>
<P>"Such liberal luminaries as Democratic Whip Nancy Pelosi, the House's leading advocate for human rights; Barney Frank, the usually remorseless critic of House follies; and John Lewis, who spent much of his youth in jail for civil rights; were among the 352 who voted aye for [Republican Whip Tom] DeLay's flier in diplomacy," wrote <I>Washington Post</I> columnist Mary McGrory.</P>
<P>A few days later, House Majority Leader Dick Armey took to the airwaves to declare that he was "content to have Israel grab the entire West Bank"--and that "the Palestinians should leave." The U.S. mainstream media barely reported this call for ethnic cleansing from one of the half-dozen most powerful Republicans in Washington.</P>
<P>At the White House, the Bush administration barely tried to appear evenhanded. It even withdrew support for a United Nations investigation of the slaughter in Jenin--in exchange for Israel's lifting of its month-long military siege of Yasser Arafat. Suddenly, the fact-finding mission that the U.S. had supported a few days earlier had become "a distraction from progress toward peace," the <I>New York Times</I> reported.</P>
<P>Administration officials occasionally bemoan the construction of Israeli settlements in the Occupied Territories, a blatant violation of international law. But they save their real fire for denouncing Arafat.</P>
<P>Sharon knows that the billions in U.S. economic and military aid will keep rolling in. This is the president, after all, that called him "a man of peace" just as the horror of Jenin was being broadcast to the world.</P>
<P>Sharon knows that he has a partner in war crimes in George W. Bush. Bush oversaw the killing of thousands of civilians in Afghanistan--in the name of "fighting terrorism." And he has far worse horrors in store for Iraq--an all-out invasion to topple Saddam Hussein that will add to the 1 million-plus death toll that the U.S. government is responsible for after a decade of war.</P>
<P>Bush doesn't care about justice for Palestinians any more than Sharon. The differences between them are disagreements between friends--the senior and junior partners in an empire in which the U.S. relies on Israel to protect its economic and military interests in the oil-rich Middle East.</P>
<P>We can't let these two war criminals cause more atrocities. We have to take on the politicians--Democrat and Republican alike--who support and bankroll Israel's war on Palestinians.</P>
<P>We have to stand up against the U.S. war machine that's putting its forces into place for a new war on Iraq. The time to organize is now.</P>

