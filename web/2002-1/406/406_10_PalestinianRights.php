<P><font face="Times New Roman, Times, serif" size="5"><b>Defend Palestinian rights</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 10, 2002 | Page 10</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"THE ONLY thing I can compare it to is Ground Zero." That's how Zaid Khalil described the destruction he saw during his trip to the Jenin refugee camp in mid-April.</P>
<P>On April 27, more than 100 people gathered in Judson Memorial Church in New York City to hear activists from the International Solidarity Movement speak about their recent visit to the West Bank.</P>
<P>"The destruction of the camps, of people's homes, of a whole community and its resources is unimaginable," Khalil told the crowd. "It's our responsibility to speak out, to let our government know that we oppose these barbaric policies," said Jordan Flaherty, who also traveled to the region.</P>
<P>Activists at Hunter College in New York showed that same spirit of opposition on May 2, when they turned out to protest an appearance by right-wing Pentagon hack Daniel Pipes. When activists put out a flyer announcing the protest, Pipes put it in his electronic newsletter, and the activists received a flood of racist hate mail--as well as threats of physical violence.</P>
<P>But they were not deterred. A group of 40 protesters greeted Pipes, chanting "You can kill, you can lie, Palestine will never die." They booed and heckled Pipes throughout his speech.</P>
<P>Across the country, college and community activists have similar stories to tell. At the University of Vermont, student activists organized a speak-out on April 30 in support of the liberation of Palestine that drew 65 people. "We know the problems that racism and ethnic divides create," said Sophia Strosberg, a Jewish anti-Zionist student on campus. "We should be doing all in our power to stop it."</P>
<P>In Austin, Texas, 200 Palestinian supporters protested a pro-Israel demonstration. A few days later, more than 80 people attended a teach-in aimed at building the movement to divest from the state of Israel. "The same racial oppression and segregation that we have seen in this country is enshrined in Israel's constitution," Palestinian activist Rania Masri told the crowd.</P>
<I><P>Kathleen Brown, Warren Craig, Aaron Hess and Brendan Sexton III contributed to this report</I>.</P>

