<P><font face="Times New Roman, Times, serif" size="4"><b>Marching for a moratorium in California</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Terzakis</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SACRAMENTO, Calif.--On May 1, Californians for a Moratorium on Executions (CME) held their first annual Moratorium Day in the state's capital. More than 250 abolitionists gathered at the state courthouse and then marched to the capitol building.</P>
<P>Though organizers initially wanted a "silent procession," the silence was broken when family members of death row inmates began chanting "It's racist, it kills the poor, we won't take it any more!"</P>
<P>A press conference and rally were held at the capitol with celebrity speakers, including civil rights leader Rev. James Lawson, actor Ed Asner and James Cromwell from <I>The Green Mile</I>.</P>
<P>Moratorium petitions with more than 85,000 signatures were given to a representative of California Gov. Gray Davis. CME issued the statewide petition for a moratorium and has worked with local groups to get moratorium resolutions passed in cities and counties around Northern California, including Santa Clara County, San Francisco, Santa Cruz, Oakland and Berkeley.</P>
<P>While some of the participants stressed repeatedly that the presenting of the petitions to Davis was not meant as an attack on the governor, others carried giant signs with pictures of Davis peering over a set of crossed syringes and the slogan, "Stop him before he kills again!"</P>
<P>The event was a step forward for the California moratorium movement and evidence that more abolitionists now see activism as the best way to end the death penalty.</P>
<P>Abolitionists now need to come together and figure out a next step--especially how to build more support in Southern California, where the movement is considerably weaker.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Defend civil liberties</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mike McHugh</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MADISON, Wis.--About 150 people braved a bone-chilling rain for a march and rally at the State Capitol to defend civil liberties April 27.</P>
<P>Marchers chanted "Free, free the detainees," and "No more racist fear! Immigrants are welcome here!" to show solidarity with Arabs and Muslims targeted by Bush's "war on terrorism."</P>
<P>Other demands of the rally included the repeal of the USA-PATRIOT Act, amnesty for undocumented immigrants and support for state and federal bills that would mandate gathering data on racial profiling.</P>
<P>Madison Alderperson Brenda Konkel urged the crowd to fight Madison's so-called anti-loitering ordinance, which has been used to target predominantly minority communities in Madison. The rally also showed strong support for the struggle of the Palestinians against Israeli occupation, as several Palestinian flags were visible at the rally.</P>

