<P><font face="Times New Roman, Times, serif" size="4">Bush's EPA drops strip mine rule</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>A gift to corporate polluters</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 10, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WHEN IS a stream not a stream? When it's a trash can for the mining industry.</P>
<P>Last week, George W. Bush's Environmental Protection Agency (EPA) changed provisions of the 1972 Clean Water Act to allow coal mining companies to shear off the tops of mountains to reach coal deposits--and then bulldoze the leftover rock and dirt into surrounding valleys and waterways.</P>
<P>Environmentalists estimate that mine owners have already wiped out more than 1,000 miles of waterways in the eastern U.S. with this strip mining technique. But it's cheaper for companies to destroy the environment--which means more profits. And profits--especially for the oil, gas and coal boys--come first at the Bush White House.</P>
<P>"This is a 'Friday Night Massacre' for our nation's waters," said Joan Mulhern, the senior legislative counsel for the environmental group Earthjustice. "[The coal operators'] lavish contributions to the Bush-Cheney campaign have just been paid back."</P>
<P>The White House had the gall to set up photo ops for Earth Day last month, showing Dubya stomping through the woods with an ax. But behind the scenes, administration officials have been taking an ax to antipollution laws. That's at the request of corporate lobbyists who have had a direct line to the White House since the Toxic Texan took over--via Energy Department officials like Joseph Kelliher.</P>
<P>In an e-mail last year, Kelliher asked energy industry lobbyist Dana Contratto to come up with a wish list. "If you were king, or Il Duce, what would you include in a national energy policy, especially with respect to natural gas issues?" he asked.</P>
<P>That e-mail was finally made public last month, after public watchdog groups sued the White House to obtain documents from Dick Cheney's energy issues task force. The 400 pages of new documents, along with more than 10,000 pages already released, underline the fact that White House energy policy was bought and paid for by corporations.</P>
<P>Among the documents that the White House finally released after a long legal wrangle is a "sample executive order"--drafted by the American Petroleum Institute--which calls for increased oil drilling and mining. Bush issued an order nearly identical to this "sample" in May 2001.</P>
<P>The White House lent a helping hand to its friends in the nuclear power industry as well when Bush designated Yucca Mountain in Nevada as a nuclear waste dumpsite. The administration ignored concerns about the safety of transporting the material across the country--and evidence that Yucca Mountain, which sits on an aquifer and in an earthquake zone, is a dangerous place to store radioactive waste.</P>
<P>The fact is that our safety and health don't concern Bush and his buddies. They don't care, for example, about the American Lung Association report issued last week, which shows that nearly 400 counties in the U.S. have smog levels above legal limits.</P>
<P>Not because there aren't laws on the books to stop this pollution. But because the Bush administration--like the Clinton administration before it--won't enforce the laws that already exist.</P>
<P>According to the EPA's own figures, enforcing existing antipollution laws would prevent 15,000 premature deaths, 350,000 cases of asthma, and 1 million cases of decreased lung function in children every year. But where's the profit in protecting our children's health and the safety of the planet?</P>
<P>We have to say no to the Bush gang's assault on the environment.</P>

