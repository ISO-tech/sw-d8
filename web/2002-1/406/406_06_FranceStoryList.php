<font face="Times New Roman, Times, serif" size="3">"Le Pen with silk stockings." That's what then-Socialist Party Prime Minister Edith Cresson called French President Valery Giscard d'Estaing in 1991, after he spoke of an "invasion" of immigrants. But Cresson herself soon afterward stole Le Pen's clothes when she promised to charter airplanes to deport undocumented immigrants.</P>

<P><A HREF="406_06_ChiracNoStopLePen.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Why Chirac's victory won't stop Le Pen</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">People across France were rightly horrified by the Nazi Le Pen's second-place finish in the first round of French presidential elections. This drove millions to vote for the conservative Jacques Chirac to stop Le Pen. But Chirac has adapted to--and legitimized--Le Pen's rhetoric over the years.</P>

