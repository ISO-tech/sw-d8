<font face="Times New Roman, Times, serif" size="5"><b>France erupts in anger at Le Pen</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 10, 2002 | Pages 6 and 7</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>HATRED OF Nazi Jean-Marie Le Pen produced a landslide victory for incumbent President Jacques Chirac in France's May 5 election.</P>
<P>Le Pen had scored a second-place finish in the first round of voting April 21, beating out former Socialist Party Prime Minister Lionel Jospin for a spot in the run-off. But Le Pen's success produced an explosion of anger.</P>
<P>The two weeks between the first and second rounds were filled with massive anti-Le Pen mobilizations that brought some 1.5 million people onto the streets of Paris and other cities across France.</P>
<P>The upsurge shattered Le Pen's dreams of winning up to one-third of the vote. But even as disgust with France's Nazis propelled him to victory, Chirac was already pandering to Le Pen voters to win their support in upcoming legislative elections.</P>
<P>Far from settling the French political scene, the presidential election is the first stage in a stormy period of political confrontation and struggle. In this special feature, SHERRY WOLF reports from Paris on the anti-Le Pen mobilizations, while LEE SUSTAR looks at the background to France's crisis.</P>
