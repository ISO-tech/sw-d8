<P><font face="Times New Roman, Times, serif" size="5"><b>Behind the rise of Le Pen</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 10, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"LE PEN with silk stockings." That's what then-Socialist Party Prime Minister Edith Cresson called French President Valery Giscard d'Estaing in 1991, after he spoke of an "invasion" of immigrants. But Cresson herself soon afterward stole Le Pen's clothes when she promised to charter airplanes to deport undocumented immigrants.</P>
<P>Cresson's maneuvers highlight the way that the Socialist Party has by turns denounced Le Pen's racism, while stealing his policies. What's more, as the party that controlled the presidency from 1981 until 1994, under Fran&ccedil;ois Mitterand, and government ministries for 15 of the last 21 years, the Socialists bear responsibility for the economic and social misery that fueled Le Pen's rise.</P>
<P>Le Pen profited from the convergence of the mainstream parties by scoring 14 percent of the vote in the 1988 presidential elections. In 1995, Chirac was elected president with a conservative legislative majority--but Le Pen increased his vote to 15 percent.</P>
<P>Yet within months, virtually the entire public sector went on strike, defeating the right's plan to privatize state-owned companies and slash social spending. Chirac called new legislative elections in 1997 to try to recapture the initiative--but the Socialists, led by Lionel Jospin, won instead.</P>
<P>Jospin, a former education minister under Mitterand, had a reputation for honesty. He allied with the Communist and Green Parties to create a "plural left" in order to refresh the Socialist Party's image.</P>
<P>Once in office, Jospin delivered on the promise of a 35-hour workweek to create jobs. But, mainly, his government carried out the conservatives' program of privatization, along with tax cuts for big business.</P>
<P>During the campaign, Jospin and Chirac tried to outdo one another on law-and-order, immigrant-bashing policies. "Unfortunately, blame for highlighting an often twisted debate on law and security was due more to the strategy of Mr. Le Pen's moderate opponents than media obsession," Britain's <I>Independent</I> newspaper noted.</P>
<P>"[Jospin and Chirac] gave weight to Mr. Le Pen's theories by placing crime at the top of their manifestoes and promising measures that moved into the National Front populist territory."</P>

