<P><font face="Times New Roman, Times, serif" size="4">Harvard University president attacks Cornel West</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Black professors stand up to racism</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 25, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P></B>
<P>Lawrence Summers, the treasury secretary during the Clinton administration and now president of Harvard University, has really put his foot in his mouth this time.</P>
<P>Two weeks ago, Harvard's most prominent Black professors announced that they might leave the university. They say that Summers has created a racist and hostile attitude toward Black faculty and affirmative action on campus. In particular, they cited Summers' treatment of prominent socialist professor Cornel West.</P>
<P>Faculty members say that in a meeting with him last October, Summers told West he needed to take on more "serious scholarship." Outrageously, Summers criticized West for working on Bill Bradley's 2000 presidential campaign and for his current support for New York politician Rev. Al Sharpton. Summers also attacked West for releasing a rap CD and accused him of grade inflation in his classes.</P>
<P>Now Summers is scrambling to make amends and prevent a mass exodus of Black professors. But he may not succeed. Adding to Summers' woes, Sharpton is also considering suing Harvard for trying to interfere with his presidential campaign, and Jesse Jackson is calling on Harvard to host a nationwide forum on affirmative action.</P>
<P>"It struck me as frightening that here&#133;at one of the most respected educational institutions in the world, the president of the university would in some way question a faculty member's private political associations," Sharpton told the <I>Boston Globe</I>.</P>
<P>Not that any of this is really surprising for racist egomaniac Summers. This is the man who wrote in 1991 that the World Bank should advocate advanced industrial countries dumping their toxic waste in poor countries because people there don't live long enough to get cancer. "The economic logic behind dumping a load of toxic waste in the lowest-wage country is impeccable, and we should face up to that," Summers wrote.</P>
<P>Now we have a toxic waste problem in Cambridge to get rid of.</P>
<B><P>Annie Levin,</B> Boston</P>

