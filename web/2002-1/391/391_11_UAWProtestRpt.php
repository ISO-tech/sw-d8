<P><font face="Times New Roman, Times, serif" size="4">Accuride workers tell UAW members at Kentucky Ford plant:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"You could be next in line"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 25, 2002 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>LOCKED-OUT Accuride workers and other United Auto Worker (UAW) activists picketed the Louisville, Ky., Ford truck plant January 14, while protests also took place at Detroit UAW headquarters and at a Janesville, Wis., General Motors plant.</P>
<P>The pickets were to protest the UAW International Executive Board's decision to cut off strike benefits for the second time since the Accuride lock-out began four years ago. The plants use scab wheels from Accuride--and the Ford plant is represented by the home local of UAW presidential candidate Ron Gettelfinger, who personally authorized the Accuride strike.</P>
<P>MIKE and ANNA STITH were among the Local 2036 members who made the trip, supported by members of Local 2488 in Bloomington, Ill., and others. Here is Mike's report.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>WHEN WE got to the Ford plant, we positioned ourselves at the gates along the road with our informational signs. The employees were amazed that we had been out four years and they didn't know it. Most of the ones we talked to were very disappointed in the union and were sympathetic to our cause. There were a lot of "Hang in there, brothers."</P>
<P>We hadn't been there long when a security guard came out and told us at each gate that we would have to leave. We made a collective decision to stay until the police or someone with authority arrived. We distributed several hundred fliers, showed our signs to the passing traffic and felt proud to be standing up for what we believed in.</P>
<P>Soon we saw two guys coming across the parking lot toward us. I assumed they were security. As they approached, one of us handed them a flier and tried to explain what we were doing.</P>
<P>One crumpled the flier in his hand without even looking at it and told us to leave. I then noticed the UAW logo on their shirts. I was shocked. I said, "You are members of the union?"</P>
<P>They told us they were committeemen. They informed us that they knew they had been installing scab parts these past years. They said the International had tried repeatedly to negotiate a good contract for us and we kept turning it down.</P>
<P>One of them said that we didn't want to work--and that we were after a contract like the "Big Three" had and that's why we were still out. "After four years, what are you trying to accomplish?" he asked.</P>
<P>I tried to ask him, if the great pioneers of our union had thought that way when clubs and guns threatened them, where would unions be now?</P>
<P>"Oh, that's different," he said. But we are still standing for what we believe and won't give up.</P>
<P>When we got to the UAW union hall, we were intercepted at the door and told in no uncertain terms that the hall was closed and we were not welcome. We just asked to use the bathroom before heading home. "You have to let us know the day before if you want to use the hall. We close the building every day at 4 o'clock."</P>
<P>I think people would have a hard time believing that fellow union brothers would treat us in such a disrespectful manner. We left. The hall was still open an hour or so later when we came back by.</P>
<P>I think we accomplished what we set out to do. Some will believe us; others won't. I only hope that their local doesn't try to poison its members minds to our plight.</P>
<P>In reality, we were there to help them realize that it could be them next. It's a crying shame that, by the time they realize it, they'll be in our shoes.</P>

