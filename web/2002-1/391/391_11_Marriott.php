<P><font face="Times New Roman, Times, serif" size="5"><b>Marriott workers fight for NYC jobs</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Petrino DiLeo</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 25, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Chanting "Marriott stop the firing! Time to do some hiring!" more than 200 workers from the destroyed Marriott World Trade Center rallied last week. Many of the workers, some of whom risked their lives to help evacuate the 22-story building before it collapsed, had worked at the hotel for 20 years.</P>
<P>Workers, some carrying signs that read, "September 11th's hidden victims," had four demands: that all displaced workers get jobs at a Marriott facility at their previous salaries; full benefits from a fund Marriott started for the workers; decent working conditions free of harassment; and counseling services for all employees affected by the disaster.</P>
<P>"We're going to continue to fight until they listen to us," Manuela Colon, who worked at the Marriott World Trade Center for 20 years, told <I>Socialist Worker</I>. "We'll go in front of every Marriott in the city. We'll go every week if necessary."</P>
<P>Marriott laid off more than 500 workers--members of Hotel Employees and Restaurant Employees Local 6--claiming it had no positions for them. But it's become clear that Marriott is using September 11 as an excuse to get rid of the union.</P>
<P>In addition to several hotels it already operates in the city, the chain is opening three new hotels in the next year. Yet, instead of bringing back workers from Local 6, it has hired nonunion employees and offered wages of $8 per hour--a far cry from the union wage of $17 per hour with benefits, including health care and four weeks of vacation.</P>
<P>Adding insult to injury, Marriott--which cashed in on part of the $20 billion "stimulus package" that Congress approved for corporations in New York City--gave employees just four days severance pay for each year worked.</P>
<P>Moreover, the company has yet to turn over any of the money it raised from private donations in the form of the "Marriott Associates Assistance Fund." "They told us that that's what our severance pay was," Dennis Best, another 20-year employee, told <I>Socialist Worker</I>. "They promised to find us jobs, and yet haven't contacted any of us. "The government has given them money, and yet the people who lost their jobs are getting nothing."</P>
<P>This from a corporation that operates more than 2,400 hotels worldwide and posted $20 billion in sales revenues last year!</P>
<P>"Every time we've gone in and asked them about it, they've told us they don't have our money," said Marlene, who had worked at the Marriott for 16 years. "We're going to keep fighting until we get our jobs back. They have the money; they have the jobs. They just won't do it&#133;They made us work like horses for them and then they just threw us away."</P>

