<P><font face="Times New Roman, Times, serif" size="5"><b>Frame-up trial of Black Power leader</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Keeanga-Yahmatta Taylor</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 25, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>JAMIL AL-AMIN, the former 1960s Black Power leader then known as H. Rap Brown, is on trial for his life in Atlanta this month.</P>
<P>Al-Amin is accused of shooting two Fulton County sheriff's deputies, killing one. Prosecutors claim that Al-Amin "ambushed" the two deputies, who were trying to serve him with an arrest warrant for the "crime" of driving without insurance and for receiving stolen property and impersonating a police officer. But this case is full of holes.</P>
<P>To begin with, why would a devout Muslim cleric--who even his enemies credit with cleaning up the formerly drug-plagued West End Atlanta neighborhood where he lived--shoot two cops over these misdemeanor crimes?</P>
<P>The surviving officer says that he and his partner wounded whoever shot them, and police even found a blood trail leading away from the scene of the crime. Problem: When the cops found Al-Amin, he was uninjured.</P>
<P>Al-Amin believes that the government is trying to set him up because of his political activities. And though prosecutors laugh this off, the fact remains that the FBI has compiled a 44,000-page file on Al-Amin dating back to the 1960s. In 1995, police tried to frame Al-Amin for another shooting--by threatening the victim with jail if he didn't finger Al-Amin.</P>
<P>Opponents of racism and police violence are mounting a campaign to demand Al-Amin's freedom--and expose the fact that this case is about racist police who have an ax to grind with a former revolutionary.</P>

