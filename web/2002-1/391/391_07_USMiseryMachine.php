<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Causing misery around the world</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 25, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SINCE SEPTEMBER 11, the mass media has been awash with the refrain: "Why do they hate us so much?" When asked why the U.S. has been the target of terrorists, George W. Bush responded, "Like most Americans, I just cannot believe it, because I know how good we are."</P>
<P>Last week, <I>New York Times</I> columnist Nicholas Kristof posed the question once again. "We're accused of inflicting Big Macs, Microsoft Windows and Julia Roberts on the helpless masses," he wrote. "These are crimes?"</P>
<P>The crime is that, although the facts are readily available, reporters like Kristof keep their U.S. readers wondering why so many of the world's people resent the arrogance and bullying of the U.S. government.</P>
<P>Most Americans are achingly familiar with the 3,200 victims of the September 11 attacks on U.S. soil. Yet how many are aware that more than 10 times that number of children worldwide also died on September 11--of hunger--and that the same number starve to death each day? More than 1 billion people in the world live on less than $1 a day. The same number lack access to safe drinking water, and 3 billion lack access to sanitation.</P>
<P>A 1998 report from the United Nations Development Program estimated the annual additional cost to provide basic human needs to the world's population: $9 billion for clean water and sanitation; $13 billion for basic health care and enough food to eat; $6 billion for basic education; $12 billion for reproductive health care for all women.</P>
<P>That adds up to $40 billion--a small price to pay to end needless human suffering the world over. That amount seems an even smaller investment when compared with the $1 billion a day that the U.S. will spend on its military this year--not to mention the $5 trillion it has spent on nuclear weapons alone since the Second World War.</P>
<P>A $40 billion price tag pales even in comparison to the $200 billion the U.S. government agreed to pay Lockheed Martin in October for a new fleet of F-35 fighter jets. Counting the $20 billion in emergency funds offered to the Pentagon after September 11, U.S. military spending for the next year will be roughly $363 billion, an increase of $54 billion over last year--and only a small fraction of it is related to the much-ballyhooed "war against terrorism." Even before September 11, the U.S. spent 18 times more on its own military than the combined spending of all its designated "enemies"--Iraq, Iran, Cuba, Libya, North Korea and Syria.</P>
<P>Yet when it comes to humanitarian aid to poor countries, the U.S. holds the distinction of the stingiest donor among the members of the Organization for Economic Cooperation and Development. Although it is the world's richest country, the U.S. spends only 0.1 percent of its gross domestic product in international aid--a miniscule amount even compared to the United Nations' modest recommended target of 0.7 percent.</P>
<P>Yet the U.S. has balked at the notion of increasing its contribution to help to halve world poverty this year, as recommended by the International Development Association, the arm of the World Bank that lends money to the world's poorest countries.</P>
<P>And while the U.S. spent $1 billion a month on its war in Afghanistan, it has shown no urgency to channel funds to help the country's people recover from the devastation of bombing. The Pentagon's well-publicized "food drops" fed less than 1 percent of Afghanistan's starving population throughout the height of the bombing last fall.</P>
<P>And now the U.S. has been slow to provide funds even to stabilize the interim government it installed in Afghanistan, where more than 235,000 government workers have not been paid for the last six months.</P>
<P>Far from alleviating terrorism, U.S. policies perpetuate it. The U.S. government polices the world with its enormous military to ensure its global dominance--ensuring massive profits for its ruling class at the expense of the world's poor. The people of Afghanistan are only its latest victims.</P>
<P>Americans deserve to know the truth about the atrocities committed in their names.</P>

