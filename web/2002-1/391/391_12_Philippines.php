<P><font face="Times New Roman, Times, serif" size="4">"Advisers" sent to Philippines</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Stop Bush's war machine</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 25, 2002 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>NICOLE COLSON reports on the latest stage in Bush's war.</P></B>
<P>"MILITARY ADVISERS." The phrase that conjures up images of the U.S. war on Vietnam has been dusted off.</P>
<P>The Bush administration is sending 660 "military advisers" to train the Philippine army in its war on Abu Sayyaf, an Islamist group fighting an armed struggle against the government. The U.S. claims that Abu Sayyaf has ties to Osama bin Laden's al-Qaeda network--and this justifies sending military personnel, as well as more than $100 million in equipment, including eight helicopters, a C-130 cargo plane and 30,000 M-16 rifles.</P>
<P>But as the first wave of "advisers" arrived last week, protests broke out--including a January 20 demonstration of hundreds outside the presidential palace in Manila. Government officials had to promise that American forces wouldn't be allowed into the field for at least three months.</P>
<P>The talk of "military advisers" is all too transparent. "If any American soldier is killed&#133;there will be tremendous pressure in the United States to widen the so-called war in the Philippines, and we will have another Vietnam," said Jovito Salonga, former president of the country's senate.</P>
<P>But sending advisers to the Philippines is only the latest effort to widen the U.S. "war on terrorism." Halfway around the globe in Colombia, President Andr&eacute;s Pastrana's government is using Bush's war as a cover to escalate a four-decade-old conflict with antigovernment rebels.</P>
<P>Somalia has also emerged as a frontrunner for targeting in "Phase Two" of the U.S. war. Like Afghans before them, panicked Somalis began fleeing their homes for fear that U.S. bombs will fall any day.</P>
<P>In Afghanistan itself, the barbarism of U.S. military intervention has only become more obvious with each passing week. For example, press reports last week revealed that about 20 percent of the cluster bombs dropped by the U.S. failed to explode--meaning that Afghan civilians will continue to be maimed and killed for years to come.</P>
<P>According to HALO Trust, a British-based de-mining organization, seven children were killed playing with unexploded "bomblets" in a single village near Mazar-i-Sharif. "We are waiting for the bombs to be cleared from our homes," villager Abdul Fatah told the <I>Boston Globe</I>. "We are waiting, waiting, waiting."</P>
<P>Hundreds of thousands more Afghans continue to wait as well--not for bombs to be cleared, but for food and shelter. In the Dashti-Arzana refugee camp, children spend their days digging in the ground for roots to use for firewood or food, according to an Associated Press report.</P>
<P>More than 4,000 families live in the camp, huddling together under plastic tarps at night as temperatures drop below freezing. So far, the only aid that families have received is a sack of wheat, some cooking oil and firewood. But that was more than two months ago, and now there's nothing left.</P>
<P>The Bush administration claims that aid for the new Afghan government is on the way. But promises don't feed starving people.</P>
<P>This is the cruel toll of Bush's war. We can't let him spread these horrors and this misery around the world.</P>

