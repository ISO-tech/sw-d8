<P><font face="Times New Roman, Times, serif" size="4">Enron and the politicians</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Liars, crooks and con artists</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 25, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>CLYDE JOHNSON thought that Enron would reward him for his hard work. Though a single parent, he put in long days to provide for his 11-year-old son Courtney and to pay the mortgage on their modest home in a Houston suburb.</P>
<P>Then Enron went belly-up--and Johnson was told that he had 30 minutes to clear out. Now he isn't sure when he'll find work again--or how he'll keep up with the mortgage.</P>
<P>That won't be a problem for Enron CEO Kenneth Lay. Lay <I>knew</I> that Enron would reward him--but that's because he helped the company steal its way to the top and then hide the evidence.</P>
<P>Lay put in long hours. But he wasn't too busy to sell more than $100 million in company stock before he revealed that Enron was about to crash into the biggest bankruptcy ever. Meanwhile, Enron workers were laid off--and watched their retirement funds in Enron's 401(k) plans vanish.</P>
<P>Lay isn't quite out of a job--though with his schedule filling up with appearances in Houston courtrooms and Washington hearing rooms, he might wish he were. The poor fellow even had to sell two homes and some property in Aspen, Colo., last week. At a total asking price of $16 million, they were a steal.</P>
<P>But don't worry about "Kenny Boy," as buddy George W. Bush calls him. He still has a $3 million Colorado mansion to retreat to--during the long days ahead as he tries to avoid going to jail.</P>
<P>This is the stark reality behind Enron's collapse. The crooks at the top got obscenely rich and stayed that way, while workers paid for it. And no one can now believe that Enron acted alone.</P>
<P>The accounting giant Arthur Andersen, which helped Enron to cover up its shady financial shenanigans, may fall. And in Washington, D.C., politicians of both parties who wrote the laws that Enron asked for are scrambling for cover.</P>
<P>Last weekend, the spotlight turned on Vice President Dick Cheney. The Texas oilman-turned-White House puppet master was already under fire for refusing to hand over records of his dealings with Enron when he headed the White House task force on energy issues. Now it turns out that Cheney was Enron's chief enforcer in trying to squeeze $64 million out of the government of India last year--to pay off a debt on a disastrous energy project.</P>
<P>The Bush gang may be in the hot seat. But their "opponents" in the Democratic Party have a lot to hide, too. Enron handed out campaign cash to almost half the members of Congress--including 100 Democrats--and its army of lobbyists spanned the Washington spectrum, from former Christian Coalition chief Ralph Reed to Michael Lewan, former chief of staff to Sen. Joe Lieberman (D-Conn.).</P>
<P>Just about everyone in the U.S. political establishment can be tied to Enron in one way or another. But Paul O'Neill just can't figure out what the fuss is all about. Bush's treasury secretary got on national television earlier this month and actually said: "Companies come and go. Part of the genius of capitalism is, people get to make good decisions or bad decisions, and they get to pay the consequence or enjoy the fruits of their decisions. That's the way the system works."</P>
<P>Except that Kenneth Lay made the decisions, but is still enjoying the fruits--while Clyde Johnson didn't decide anything and is paying the consequences. That <I>is</I> the way the system works--and now millions of people who had never heard of Enron a few weeks ago know it.</P>
<P>The Enron scandal has exposed everything corrupt and cruel about the system of capitalism. And it shows why we need an alternative to a world where working people work longer and harder to create the obscene wealth that Lay and his pals flaunt--only to be kicked aside when they aren't needed.</P>
<P>We need a socialist society--that puts people before profits.</P>

