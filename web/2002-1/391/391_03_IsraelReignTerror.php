<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>New stage in Israel's reign of terror</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 25, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ISRAELI TROOPS occupied the West Bank town of Tulkarem early this week in the largest invasion of Palestinian Authority (PA) territory in the 16 months of the new Palestinian Intifada.</P>
<P>Israeli Transport Minister Ephraim Sneh promised more of the same. "Mainly in the northern West Bank, there is a very profound infrastructure of terrorist attacks from all organizations," he told reporters.</P>
<P>But the real terrorists are on the Israeli side. It was Israel's tanks that rumbled through Tulkarem, a city of 50,000 people, as soldiers imposed a curfew and carried out house-to-house searches, taking away dozens of young Palestinian men.</P>
<P>The town's key buildings were taken over, including a local college and the home of the mayor, and the Israeli flag was run up flagpoles. "They came and knocked on the door at about four in the morning," Mayor Mahmoud Jalad told reporters. "For three hours, they kept me, my wife and three daughters out in the cold. Then they allowed me to sit in a corner of my house with my family, and they said, 'no bathroom.'"</P>
<P>Prime Minister Ariel Sharon said that the assault was in retaliation for a suicide attack on a banquet hall in the Israeli town of Hadera. But to Palestinians, the occupation of Tulkarem is only the latest escalation of an Israeli reign of terror that has continued uninterrupted for weeks.</P>
<P>The raid was a further humiliation of PA leader Yasser Arafat, who has been confined to his West Bank headquarters in Ramallah by an Israeli siege. Sharon and his government say that Arafat is no longer "relevant"--despite the string of concessions that Arafat has made to meet Israeli demands for mass repression.</P>
<P>But in the wake of the latest offensive, Arafat gave a fiery speech vowing to win a Palestinian state with East Jerusalem as its capital. As Arafat spoke, the Israeli tanks that ring Ramallah were dug in only a few dozen yards from his headquarters, and an Associated Press account said that "the crack of bullets fired by Israeli soldiers in clashes with stone-throwing youths could be heard" during the speech.</P>
<P>Many Palestinians have united behind Arafat in the face of Israel's attacks. But he remains despised for his continual concessions to Israel. The new slaughter has only stoked the fury of Palestinians, who responded to the invasion of Tulkarem with resistance--even when they had only stones to fight back.</P>
<P>This occupation is the latest example of Sharon's vision of "peace"--a permanent occupation of Palestinian land, armed and funded by Washington. We have to oppose Israel's reign of terror--and the U.S. government support that makes it possible.</P>

