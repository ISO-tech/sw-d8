<P><font face="Times New Roman, Times, serif" size="5"><b>Should we eat stones to live?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 25, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P></B>
<P>A few hours ago, I saw a television news segment that announced a 20 percent rise in the price of fuel in Indonesia. This rise will create a bad effect in another sector--public transportation.</P>
<P>The cost of public transportation, a necessity for many people, is going to rise again, making it very, very expensive. This is in spite of the fact that our government just raised the price a few months ago. This will mean that many people won't be able to afford necessities.</P>
<P>Does the government think that we should eat stones to live? The people of Indonesia are the ones who will suffer as a result of this new price increase.</P>
<P>I hope <I>Socialist Worker</I> can print my letter so that the Indonesian people who live in America can know about the harassment taking place in their own country. It still exists and it must be stopped!</P>
<B><P>Reza Irmansyah,</B> Indonesia</P>

