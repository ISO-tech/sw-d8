<P><font face="Times New Roman, Times, serif" size="5"><b>The case for ending the death penalty</b></font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>BOOKS:</B> Rev. Jesse Jackson Sr., Rep. Jesse Jackson Jr. and Bruce Shapiro, <B><I>Legal Lynching: The Death Penalty and America's Future,</B></I> The New Press, 174 pages, 2001, $22.95.</P></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Alice Kim</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 25, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CLARENCE BRANDLEY and a fellow high school custodian in Conroe, Texas, had the misfortune to discover the body of a murdered 16-year-old student, Cheryl Dee Ferguson. The two were immediately considered suspects by the police. To demonstrate his innocence, Brandley voluntarily gave the police hair and clothing samples and submitted to a lie detector test, which he passed.</P>
<P>"One of the two of you is going to hang for this," a police officer told the two during the investigation. Then, turning to Brandley, the officer added, "Since you're the nigger, you're elected." Without any physical evidence, Brandley was railroaded on to Texas death row where he languished for nearly a decade before he was freed.</P>
<P>Brandley's nightmare is chillingly similar to the stories of nearly 100 other wrongfully convicted death row prisoners who have been exonerated since the death penalty was reinstated in 1976.</P>
<P>Grounded in stories like these, <I>Legal Lynching</I>--an updated edition of the Jacksons' 1995 book--makes a powerful case against the death penalty. The authors tell stories of sleeping lawyers, all-white juries, racist judges and incompetent attorneys, and draw the conclusion that "America is executing individuals for one crime only: the crime of being poor."</P>
<P>The book also argues that "the death penalty is essentially an arbitrary punishment, a product not of blind justice but of geography and of ethnicity." As cited in the book, 11 Southern states carried out 83 percent of U.S. executions between 1976 and 1995. Furthermore, Blacks who murder whites are 19 times as likely to be executed as whites who kill Blacks.</P>
<I><P>Legal Lynching</I> also challenges some of the more prevalent notions that are used to justify capital punishment--such as the claim that the death penalty prevents crime or provides closure for family members of murder victims.</P>
<P>The authors show that the death penalty--far from being an uncontested fixture of American society--has been challenged by abolitionists throughout U.S. history. Consequently, various states outlawed the death penalty. And in 1972, the Supreme Court ruled the death penalty an unconstitutionally cruel and arbitrary punishment--a ruling that was upheld for four years.</P>
<P>The book ends with a call for a national moratorium on executions and includes the complete text of the National Death Penalty Moratorium Act of 2001, a bill introduced by Rep. Jesse Jackson Jr.</P>
<P>Although the book captures the growth of anti-death penalty sentiment today, it doesn't address the critical role that activism has played in shifting the tide. And it offers life in prison without parole as an alternative--a concession to law-and-order politics that paved the way for more executions throughout the 1990s. There's another problem, too: The book has some prominent factual errors, so check the statistics before you use them.</P>
<P>Nevertheless, this book is a useful tool for abolitionists--and for anyone questioning the death penalty.</P>
