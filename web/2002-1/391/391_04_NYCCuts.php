<P><font face="Times New Roman, Times, serif" size="5"><b>New York politicians make cuts that will kill</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 25, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P></B>
<P>Hungry elderly immigrants? They don't need to eat. Cut $6.5 million in food aid out of the New York City budget.</P>
<P>Infant mortality? Tough luck. Cut programs to prevent infant mortality by one-third.</P>
<P>Looking for day care? Keep looking. Eliminate 10,000 day-care openings for kids.</P>
<P>These are just a few of the grotesque budget cuts approved in December by the Democrat-controlled New York City Council and outgoing Republican Mayor Rudolph Giuliani.</P>
<P>But wait, doesn't New York City have a newer, gentler mayor: Mike Bloomberg? Not only has Bloomberg refused to criticize any of the cuts, he's demanded that city agencies cut another 20 percent.</P>
<P>No leading Democrat has objected to these deadly attacks, which include chopping in half money spent on AIDS prevention.</P>
<P>As for the "budget deficit" con game, city workers know that in good times, the government gives huge tax cuts to the people on top. Then, in bad times when city receipts fall, they choose to make cuts that kill people who can't afford to buy the basics.</P>
<P>In December, Bellevue Hospital union workers were counting down the days until Rudy Giuliani got out of City Hall. On the first working day of January, there were some mild expressions of hope that Bloomberg might be better.</P>
<P>But by the end of the first week in January, Bellevue workers were talking about "Mike the Knife"--and how if he thinks he can treat city workers like dirt, he better watch out. Just about everybody mentioned what happened to the budget cutters in Argentina.</P>
<B><P>Thomas Barton,</B> New York City</P>

