<P><font face="Times New Roman, Times, serif" size="5"><b>Will Boeing's bedbugs bite?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 25, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHINESE PRESIDENT Jiang Zemin is getting bugged by the U.S. In August, the aircraft maker Boeing delivered a jet specially outfitted for Jiang. But during test flights, the jet made a strange static whining noise.</P>
<P>It turns out that 27 satellite-operated bugs were spread throughout the plane, including in the upholstery, the bathroom--and the headboard of Jiang's bed.</P>
<P>The news came a month before George W. Bush was supposed to go to Beijing for a summit. U.S. officials had little to say, but no one doubted that the reports were true. "We all do it--even when we're friends," a retired U.S. intelligence agent said.</P>
<P>After bombing the Chinese embassy in Belgrade during the 1999 war against Yugoslavia and refusing to apologize when a U.S. spy plane clipped a Chinese jet last year, you might think that the U.S. would try to play its spy games with China a little more carefully.</P>
<P>But Sen. Fred Thompson (R-Tenn.) will have none of that. "We've got to do some kind of aggressive things," Thompson said, "and, frankly, our intelligence community I think has not suffered from being overly aggressive the last several years, just the contrary."</P>

