<P><font face="Times New Roman, Times, serif" size="4">Two trigger-happy troopers in N.J. walk free</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Green light for racial profiling</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte and Yusuf Michaels</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 25, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>A NEW Jersey court gave police the green light for racial profiling January 14 when it let off two trigger-happy state troopers with a slap on the wrist.</P>
<P>The two white cops, John Hogan and James Kenna, were originally charged with aggravated assault and attempted murder for firing a hail of bullets at four unarmed Black and Latino passengers traveling in a van that the troopers had pulled over on the New Jersey Turnpike.</P>
<P>Three of the four young men in the van were wounded, and last year, the state paid the victims $13 million to settle a civil lawsuit. But last week, a judge allowed Hogan and Kenna to plead guilty to misdemeanor charges--sparing them from jail sentences and even probation. The cops walked free with fines of just $280 each.</P>
<P>The shooting sparked a national outcry in 1998 and focused attention on police use of racial profiling after the troopers admitted that their superiors had instructed them to target Black and Latino drivers. State police superintendent Carl Williams was forced out in 1999 after he said in a newspaper interview that minorities were largely to blame for drug trafficking.</P>
<P>The New Jersey case forced the issue of racial profiling into last year's election campaign, where even George W. Bush had to speak out against the practice. But Bush's "war on terror" after the September 11 attacks has shifted racial profiling from a dirty secret of police forces to official policy.</P>
<P>Law enforcement officials are encouraged to target Arabs and other Middle Eastern people in the name of "security." The dirty deal for Hogan and Kenna is another piece of that picture.</P>
<P>"Now they are trying to decriminalize racial profiling," Dedrick Muhammad, of Rev. Al Sharpton's National Action Network (NAN), told <I>Socialist Worker</I>. "Under civil rights law, they should be persecuted as criminals, not slapped on the wrist by the courts, and get disciplinary action by their institution."</P>
<P>Last weekend, NAN held a demonstration in Washington, D.C., where about 600 people--including Arabs, Muslims and African Americans--came together to oppose racism. We can build on demonstrations like these to expose these trigger-happy police.</P>

