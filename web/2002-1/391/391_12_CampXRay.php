<P><font face="Times New Roman, Times, serif" size="5"><b>Barbaric conditions at Camp X-Ray</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 25, 2002 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>U.S. officials thought it would be a good photo op. But the pictures of suspected al-Qaeda fighters in custody at Camp X-Ray in Guant&aacute;namo Bay, Cuba, have provoked outrage across the world.</P>
<P>The photos show men forced to kneel on rocky ground, their hands and feet bound, their eyes covered with blacked-out goggles and their mouths and noses masked. The prisoners are housed in 6-by-8-foot chain-link cages, exposed to the elements. Toilet facilities consist of a bucket. Only one of their meals each day follows Muslim dietary law.</P>
<P>U.S. officials have been careful not to refer to the 158 men in Camp X-Ray as "prisoners of war." To call them this would mean that they would be entitled to certain minimum standards of treatment under the Geneva convention.</P>
<P>Instead, the U.S. classifies the prisoners as "unlawful combatants"--meaning that they can be brutalized and humiliated with no respect for their basic human rights. This despite the fact that not one prisoner has been convicted or even charged with any crime.</P>
<P>This disgusting abuse prompted an outcry from humanitarian groups--and even complaints from U.S. allies, including officials in the lapdog British government of Prime Minister Tony Blair.</P>
<P>But Pentagon brass couldn't care less. Secretary of Defense Donald Rumsfeld sneered at reporters' questions about the conditions, and insists that Camp X-Ray will be expanded to hold as many as 2,000 people.</P>

