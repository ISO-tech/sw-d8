<P><font face="Times New Roman, Times, serif" size="5"><b>Biting comment on the Bosnia war</b></font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>MOVIES: <I>No Man's Land,</I> </B>written and directed by Danis Tanovic, starring Branko Djuric and Rene Bitorajac.</P></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Paige Sarlin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 25, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><I><P>NO MAN'S Land</I>, set in 1993 during the Balkans War, is a welcome antidote to the pro-war Hollywood films in theaters today.</P>
<P>It takes place during the height of the United Nations' (UN) disastrous "humanitarian" aid mission and six years before the U.S.-led NATO bombing campaign would wreak untold damage and worsen an already desperate refugee crisis.</P>
<P>The story centers around two soldiers--a Bosnian and a Serb--who are trapped together in a trench between relentless gunfire from both fronts. They share the trench with a Bosnian soldier who has an American-made jumping landmine planted beneath him that could explode if he moves. The waiting game in the trench occupies most of the film, as UN forces are called in to mediate the stalemate and an international media blitz ensues.</P>
<P>Humorous and brutal at the same time, Bosnian director Danis Tanovic illustrates how soldiers are just pawns in a complicated game that has nothing to do with their own interests. While exposing the savagery of war, Tanovic directs his satire mostly at the international community and the UN, focusing in on their futility and ineffectiveness.</P>
<I><P>No Man's Land</I> doesn't answer questions U.S. audiences might have about the causes of the war. But it hints at the need for a further examination beyond the mainstream media's portrayal of the war in the Balkans when the soldiers have a comical exchange over who began the war.</P>
<I><P>No Man's Land</I> is an indictment of the UN force, the international community and the media, whose hunt for ratings is more important than the depiction of actual events.</P>

