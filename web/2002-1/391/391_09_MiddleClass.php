<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Between labor and capital</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 25, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>FOR MARXISTS, the working class is central to achieving a different world--socialism. It is labor that produces profits--the life's blood of capitalism. Workers therefore have a great deal of leverage, if they organize collectively, to bring down the system. Moreover, as the exploited class, workers' ultimate interests lie not in tinkering with the mechanisms of their own exploitation, but in abolishing them.</P>
<P>But between the two main classes in society, workers and capitalists, there is a middle class that consists of professionals, managers and small businesspeople--sometimes referred to as the petty bourgeoisie.</P>
<P>The upper end of this layer shares more in common in its lifestyle and outlook with the ruling class. The lower end, in terms of salary and working conditions, has more in common with the working class.</P>
<P>As a whole, the middle class vacillates between the two main classes. As a small businessperson, a shopkeeper identifies with the capitalists; as someone who labors long hours to make ends meet, the shop owner has more in common with wageworkers.</P>
<P>If the class outlook of the working class in collectivist, the class outlook of the middle class is individualist. This isn't only true of small businesspeople, whose material conditions tend to reinforce the idea of individual merit, but also true of the "intelligentsia"--students, academics and professionals such as lawyers and doctors. Their whole education and life experience reinforces individualism.</P>
<P>Yet it is this very sense of individualism that ties them to capitalism. Writes Russian revolutionary Leon Trotsky, "The writer does not have to get up when the alarm sounds, behind the doctors back stands no supervisor, the lawyer's pockets are not searched when he leaves the court. But in return, he is compelled to sell not his mere labor power, not just the tension of his muscles, but his entire personality as a human being--and not through fear but through conscientiousness. As a result, these people don't want to see and cannot see that their professional frock-coat is nothing but a prisoner's uniform of better cut than ordinary."</P>
<P>Socialism, as Marx defined it, is the "self-emancipation of the working class," and, therefore, about collective action, power and aims. "A worker," writes Trotsky, "comes to socialism as a part of a whole, along with his class, from which he has no prospect of escaping. He is even pleased with the feeling of his moral unity with the mass, which makes him more confident and stronger."</P>
<P>The trade union bureaucracy shares some characteristics of the middle class. They are a stratum standing above workers, with higher salaries that aren't dependent upon wage labor, and whose role as mediators between labor and capital not only tends to make them more conservative, but often puts them in closer proximity to the bosses with whom they negotiate than with the rank and file.</P>
<P>The middle class is, as a class, incapable of offering its own solution to the crisis of capitalism. In the battle for socialism, it can only play an auxiliary role. It can be drawn into struggle in times of crisis--drawn by anger or despair either to the right (as in Germany in the 1930s), or to the left, as in Argentina, where sections of the middle class joined workers and the unemployed to bring down the government.</P>
<P>The revolt in Argentina, is brilliant proof of the working class's role in society. But while the working class and middle class can together bring down a government, only working-class leadership in the movement can lead the struggle forward.</P>
<P>That requires that the working class organize independently into a revolutionary party rooted in workplaces and among the unemployed, in order to turn a revolt against the effects of neoliberalism into a revolt against the entire system.</P>

