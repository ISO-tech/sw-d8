<P><font face="Times New Roman, Times, serif" size="4">While Bush schmoozes with Hoffa...</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Administration goes after unions</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 25, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH spent last weekend schmoozing with Teamsters President James P. Hoffa. The two have been getting on famously--with the Bush administration easing government oversight of the Teamsters and Hoffa trumpeting the union's support for Bush's plan for oil drilling in the Arctic National Wildlife Refuge.</P>
<P>But don't think that Bush is offering to "scratch your back, if you scratch mine." Union members should know that he's got a knife with him. That same week, Bush made a series of anti-union attacks.</P>
<P>At United Airlines, where 15,000 union machinists voted almost unanimously to strike over management's refusal to budge during a two-year contract fight, Bush's presidential emergency board recommended that the White House extend executive intervention, blocking the strike for another 30 days.</P>
<P>Also last week, Bush signed an executive order barring Justice Department employees from unionizing--in the name of "national security." Federal workers who are fortunate enough to be in unions are already barred from striking.</P>
<P>"We're outraged by this," said Steven Kreisberg, associate director for collective bargaining at the American Federation of State, County and Municipal Employees, which represents more than 300 Justice Department employees, including secretaries, file clerks and messengers.</P>
<P>This comes a week after Bush made Eugene Scalia, son of right-wing Supreme Court Justice Antonin Scalia, the Labor Department solicitor in a recess appointment that will save him from the congressional confirmation process. Scalia, a corporate lawyer, has said that ergonomics regulations to reduce workplace injuries were based on "quackery" and "junk science."</P>
<P>With friends like Bush, labor doesn't needs any enemies.</P>

