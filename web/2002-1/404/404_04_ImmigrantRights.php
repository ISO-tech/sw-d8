<P><font face="Times New Roman, Times, serif" size="5"><b>We can't let Bush trample immigrant rights</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 26, 2001 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>In January 1941, President Franklin Roosevelt ordered the mass removal of 110,000 citizens and noncitizen immigrants of Japanese origin, following the bombing of the U.S. naval base in Pearl Harbor. Removal was indiscriminate. All Japanese men, women and children were perceived as an imminent threat to the national security of United States citizens--white citizens.</P>
<P>Detainees at the 10 concentration camps were denied all of their civil rights and liberties. They were held for as long as three years. Some were even ordered to fight for the U.S. abroad.</P>
<P> Many people are astonished that such acts could be perpetrated in this freedom-loving country. But this hasn't prevented us from doing the same thing 61 years later--to Muslims and people of Arab descent.</P>
<P>Since October 2001, thousands of Muslims and Arabs, most of them men aged 18 to 33, have been detained by U.S. authorities. The detainees were picked up because they fit "criteria" that supposedly means they could be terrorists.</P>
<P>After the September 11 attacks, Muslim immigrants and noncitizens (even those with green cards) have been moved from prison to prison, have been denied their Constitutional rights and have been prevented from contacting their families or their lawyers. </P>
<P>Americans are quick to condemn foreign leaders who detain citizens without legitimate legal grounds and violate their human and civil rights--like Hitler or Pinochet or Jiang Zemin. Yet Americans are strangely apathetic when President Bush does the same in our country. </P>
<P>Is it for our protection that Bush is repressing the fundamental rights of immigrants and noncitizens? What is to stop him from doing the same to us?</P>
<B><P>Joya Banerjee,</B> sent by e-mail</P>

