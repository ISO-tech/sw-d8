<P><font face="Times New Roman, Times, serif" size="4">From Camp X-Ray to 9/11 detainees...</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>White House war on human rights</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 26, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH administration has gone to war on civil liberties and human rights--at home <I>and</I> abroad. Last week, Amnesty International released another damning report detailing the U.S. military's horrendous abuse of its prisoners of war at prison camps in Guant&aacute;namo Bay, Cuba, and in Afghanistan.</P>
<P>In its 62-page report, Amnesty details multiple violations of the rights of detainees, including physical abuse, lack of adequate housing--and even several cases of men who were essentially kidnapped by the U.S. government from countries like Pakistan and shipped to Guant&aacute;namo Bay for questioning.</P>
<P>But talk of "human rights" doesn't seem to matter to the State Department. According to a recent State Department document, not only do the Guant&aacute;namo prisoners have no right to a lawyer, but they can be held in Cuba for as long as the U.S. wants.</P>
<P>"The detainees have no right to counsel or to have access to courts," reads a brief, sent to the Organization of American States to defend the U.S.'s refusal to obey the Geneva convention. "If and when a detainee is charged with a crime, he will have the right to counsel and fundamental procedural safeguards."</P>
<P>The pro-torture, pro-prison camp crackpots in the Bush administration aren't just using this brutal treatment against prisoners from the war on Afghanistan. They're doing it at home.</P>
<P>According to a lawsuit filed April 17 by the Center for Constitutional Rights (CCR), detainees from the federal government's September 11 investigation have been subjected to appalling conditions. According to the CCR, dozens of detainees at the Metropolitan Detention Center in New York City have been held in maximum security and solitary confinement--without charges, or without even being accused of a crime in some instances.</P>
<P>Detainees say that they're subjected to body cavity strip searches after every visit from a lawyer. And according to the CCR, some have been beaten by guards--simply because of their religion.</P>
<P>"They're arresting people simply based on their ancestry," Barbara Olshansky, an assistant legal director for the CCR, told <I>Socialist Worker</I>. "Many of these people&#133;do not come from countries listed by our government as countries that harbor terrorists. Many of them came here from countries like Canada, France and Turkey, and are being targeted simply because of their South Asian or Muslim heritage. Furthermore, we're arresting people for immigration violations that we've never arrested people for before."</P>
<P>Meanwhile, local activists are continuing to hold weekly demonstrations outside the Metropolitan Detention Center to demand the release of the detainees. We have to fight the U.S. government's war on our rights.</P>

