<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ITALY</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">General strike against Berlusconi's attack</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"The country has come to a stop"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 26, 2001 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ITALY WAS shut down by a general strike April 16 involving some 13 million workers fighting anti-labor legislation.</P>
<P>The central squares of cities--from Turin in the North to Palermo on the island of Sicily in the South--were jammed with union members and their supporters. Rome, Florence, Bologna and Milan saw rallies of 200,000 each.</P>
<P>Public transportation was shut down, along with schools and post offices. TV networks didn't operate, and newspapers didn't publish. The country's biggest companies--including auto giant Fiat and the tire maker Pirelli--ground to a halt.</P>
<P>At the demonstrations, the red flags of Italy's three union federations and also the left-wing political parties were everywhere.</P>
<P>"The country has come to a stop," said Sergio Cofferati, secretary general of the biggest union federation, the CGIL, at a rally in Florence. "The government has to change course."</P>
<P>The mobilization for the strike went far beyond the usual activists in the labor movement. "I have seen many workers strike who had never been on strike in their entire life--or who voted for the right wing in the last elections," said one shop steward from Milan.</P>
<P>The unions are fighting Prime Minister Silvio Berlusconi's attempt to abolish Article 18, which prohibits companies from firing workers without just cause. The general strike followed a massive demonstration in Rome on March 23, when some 2 million workers rallied against the proposed law.</P>
<P>That rally took place in defiance of Berlusconi and his government, which had blamed unions for creating the "ideology" that led to the murder of a lawyer who drafted the law. While the small Red Brigades guerilla group took responsibility for the murder, no one has yet been arrested for it--and many on the Italian left suspect the involvement of the security services.</P>
<P>The size of the strike shows that Berlusconi hasn't been able to intimidate workers. But even bigger showdowns are likely in the future.</P>
<P>In 1994, a general strike and mass protests forced the resignation of Berlusconi's first government. But the center-left government that followed--led by the Democratic Left, the former Communist Party--pursued pro-business policies that opened the way for a return of the right wing.</P>
<P>This time around, Berlusconi is determined to ram his program through, and his government has the firm backing of big business. What's more, his coalition includes the neofascist National Alliance and the anti-immigrant Northern League, which have tried to whip up a racist political atmosphere.</P>
<P>But the left responded with a series of mobilizations--from the 300,000 who demonstrated against the Group of Eight summit in Genoa last July to major antiwar protests after September 11.</P>
<P>The general strike shows the potential to link the opposition to Berlusconi to the power of the organized working class.</P>

