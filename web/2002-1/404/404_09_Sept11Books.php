<P><font face="Times New Roman, Times, serif" size="5"><b>From September 11 to Bush's war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 26, 2001 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>BOOKS:</B> <B><I>September 11 and the U.S. War: Beyond the Curtains of Smoke</B></I>. Roger Burbach and Ben Clarke, eds., City Lights Books, 2002, 174 pages, $11.95. <B><I>After 9/11: Solutions for a Saner World.</B></I> Don Hazen, Tate Hausman, Tamara Straus, Michelle Chihara, eds., Independent Media Institute, 2002, 187 pages, $15.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>IMMEDIATELY AFTER the September 11 attacks, the U.S. mainstream media got to work. They pumped up their propaganda machine to help George W. Bush prepare Americans for the endless "war on terrorism" that would come.</P>
<P>In the alternative press, a wide variety of views emerged--ranging from suspicion of U.S. war aims, to progressive proposals on how to "deal with" the terrorists, to support for the war. Likewise, the stack of post-September 11 books that hit bookstores last month also reflects this political confusion.</P>
<P>Two of these books--<I>September 11 and the U.S. War: Beyond the Curtains of Smoke,</I> and <I>After 9/11: Solutions for a Saner World</I>--put together essays from various liberal and radical journalists, historians, commentators and activists.</P>
<P><I>After 9/11</I> provides a wide array of commentary--from liberal journalist Bill Moyers to hip-hop reporter Jeff Chang. This collection includes some good pieces, such as Arundhati Roy's "New World Disorder," David Corn's tracing of the CIA's funding of the mujahideen in the 1980s, and Laura Flanders on the role of alternative journalism.</P>
<P>It has an interview with AFL-CIO President John Sweeney, in which he addresses the "domestic war" on workers--one important consequence of the Bush war drive--though Sweeney has consistently supported Bush's war abroad.</P>
<P>But <I>After 9/11</I> also includes some of the worst "progressive" responses to September 11. In "Protest is Patriotic," liberal Texas radio host Jim Hightower is eager to defend his right to free speech--but not nearly as enthusiastic about people in Afghanistan. "[T]he U.S. has no choice but to go after the bastards," says Hightower. "The ruthless murderers smacked our nation and all of civilization in the face." As if the Washington war makers who dropped bombs on innocent Afghans are the "civilized" ones.</P>
<P>Equally sickening is a piece by the <I>Nation</I> magazine's Joel Rogers and Katrina vanden Heuval, who view the war on terrorism as an "opportunity" for progressives.</P>
<P>The better of these two books is the collection from City Lights, <I>September 11 and the U.S. War. </I>It offers a wealth of information on the U.S. government's rotten foreign policy record, including articles on Washington's drive to control the world's oil supplies, the CIA's role in aiding terrorism, and the U.S. government's destruction of democracy around the world, as in the 1973 overthrow of Salvador Allende in Chile.</P>
<P>Contributors include Roy, Howard Zinn, Michael Klare, Norman Solomon and Marc Herald. Plus David Potorti, of September 11 Families for Peaceful Tomorrows, makes an impassioned argument against bombing in Afghanistan.</P>
<P>This book gives many reasons to question the war on terrorism. But it doesn't offer clear suggestions about how to oppose it. It will be up to activists to make the argument against U.S. imperialism--wherever it rears its head--in the months to come.</P>

