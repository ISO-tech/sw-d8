<P><font face="Times New Roman, Times, serif" size="4">There's no middle ground in Israel's racist war</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>We stand with Palestinians</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 26, 2001 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Yves Coleman's letter to the editor (<I>SW,</I> April 19) has left me perplexed. Coleman accuses <I>SW</I> of fueling anti-Semitism because it quoted an Israeli officer who called for the Israel Defense Forces (IDF) to study the tactics used by the German Army in the Warsaw Ghetto. </P>
<P>My first question is, Did Coleman also write a letter to the Israeli newspaper <I>Ma'ariv</I> (which first reported the quote from the IDF commander), accusing them of anti-Semitism?</P>
<P>I would wager that Coleman's argument is not about anti-Semitism. He ends up apologizing for colonialism, Israeli apartheid and imperialism. He reconstructs history by claiming that Israelis are not occupying a foreign country. This is not far from the racist Zionist justification for hijacking Palestine to found Israel: "A land without a people for a people without a land."</P>
<P>Perhaps Coleman hasn't heard of UN resolution 194, which calls for the right of return for Palestinians who were forcibly evicted from their homes in what is now Israel. Perhaps he has also called "anti-Semitic" the deeds and keys that many Palestinian refugees still have to their former homes inside Israel. </P>
<P>I wonder what words he has for the recent massacre in Jenin, where tanks buried people alive in their homes? </P>
<P>The question is--as it has been since 1948--do you support Palestinian rights, or do you support colonialism?</P>
<I><P>SW</I> has a proud record of its relentless battle against anti-Semitism--and colonialism. It has continued to declare support for a single secular state, with Jews and Arabs living together with equal rights.</P>
<P>If an IDF officer chooses to use tactics employed by the German army in its massacre in the Warsaw Ghetto, then we have to celebrate the courageous fighters of the Jewish Underground Resistance who fought against the Nazis by any means necessary. </P>
<P>Their aim was freedom from oppression. That has everything to do with socialism.</P>
<B><P>Alpana Mehta,</B> Boston</P>

