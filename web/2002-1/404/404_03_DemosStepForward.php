<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">April 20 protests show a renewed opposition to Bush's wars</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>A big step forward</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 26, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE BIG turnouts for antiwar demonstrations on April 20 broke through the media blackout on opposition to Bush's war machine and its ally Israel. With about 25,000 in San Francisco and up 100,000 in Washington, D.C., there can be no doubt that the antiwar movement has been revived and broadened.</P>
<P>The demonstrations brought together a wide array of political currents organizing around many issues--including global justice activists, pacifists and student groups. The protests also saw a massive turnout by Arab Americans and Muslims, who have been scapegoated by the U.S. government since September 11.</P>
<P>This mobilization wasn't only the result of anger with the U.S. war drive, but an outpouring of solidarity with the Palestinian struggle. "We are all Palestinians" was a popular slogan among Arabs and non-Arabs alike.</P>
<P>Even the corporate media--which sneered at antiwar protests last September as "pro-terrorist"--had to take notice.</P>
<P>The protests took place as the U.S. "war on terror" has been increasingly exposed for what it is--an all-purpose label for imperialist aggression.</P>
<P>Despite occasional U.S. criticism of Israeli Prime Minister Ariel Sharon, Washington gave the green light for his military invasion of the West Bank. Washington might prefer a "peace process" to co-opt Palestinian resistance. But Israel is America's main ally in the oil-rich Middle East, and if maintaining this relationship means defending the periodic massacre of Palestinians, then Washington's answer is simple: So be it.</P>
<P>After all, the U.S. is planning to carry out a slaughter of its own--in Iraq. That's why it's so important to stand forthrightly for Palestinian self-determination in the struggle against Israel--and build a movement that can oppose U.S. imperialism everywhere. </P>
<P>Of course, the U.S. will continue to use the charge of "terrorism" to discredit all resistance--whether the issue is suicide bombings by desperate Palestinians or guerrilla war by those fighting the U.S.-backed military in Colombia.</P>
<P>But the U.S. has no problem with terror when it comes from its own warplanes over Afghanistan or Iraq or from right-wing death squads in Colombia.</P>
<P>And by backing the failed coup in Venezuela earlier this month, the U.S. exposed the reality of its claim to be fighting for freedom. "Mr. Bush's muted reaction to a coup attempt in Venezuela has led to questions about whether the Bush White House celebrates democracy only when it produces governments Mr. Bush finds amenable," the <I>New York Times</I> admitted.</P>
<P>Meanwhile, Washington's war abroad has been accompanied by a war at home on working people--a massive buildup of weaponry, while budgets are slashed for health care, schools and other urgently needed social programs.</P>
<P>That's why a number of speakers at the April 20 demonstrations argued that activists have to generalize their aims. "Globalization is neocolonialism," Egyptian author and activist Nawal El-Shadawi said after her speech at a rally outside the World Bank headquarters. "We are struggling against a military and economic system, against imperialism."</P>
<P>Building a movement that can take up this struggle will require more numbers--building organizations and making connections in every community, campus and workplace to bring together everyone opposed to Israel's attack on Palestinians and the U.S. intervention.</P>
<P>The April 20 protests show the tremendous potential for organizing opposition to the U.S. war machine--and building solidarity with people in struggle around the world.</P>

