<P><font face="Times New Roman, Times, serif" size="5"><b>Fighting for living wages at Harvard</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Petrino DiLeo</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 26, 2001 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>DOCUMENTARY: <I>Occupation,</B></I> directed by Maple Razsa Pacho Velez. For listings, visit www.enmassefilms.org.</P></font>
<font face="Times New Roman, Times, serif" size="3"><I><P>OCCUPATION</I> DOCUMENTS the three-week student occupation and mass demonstrations at Harvard University last April that won more than 2,000 university workers living wages. The struggle grew from 50 students occupying a hall into a movement of workers, students and the community that broke Harvard's will.</P>
<P>It began when dining hall workers brought pizza to the students' living-wage occupation as a sign of solidarity. Workers began holding impromptu teach-ins in the dining halls during their breaks. Then they organized protest, and eventually a strike vote.</P>
<P>Ultimately, the mass pressure of students, workers and community leaders was too much, and Harvard was forced to give all union and non-union employees a living wage.</P>
<P>The film features longtime Harvard employees who describe their harsh working conditions. With a $20 billion endowment, Harvard is the one of the wealthiest public institutions in the world. Yet its service employees were paid low wages and forced to work 15 to 20 hours a day.</P>
<P>The film shows how the occupied hall became the center for daily mass rallies, with students forming a tent city around it. Community members also came out to support the struggle, with local churches even holding services outside the hall.</P>
<P>The film's most poignant moments show workers explaining how--while they admired the students for putting themselves on the line--they knew that if they were going to get a living wage, they would have to fight for it themselves.</P>

