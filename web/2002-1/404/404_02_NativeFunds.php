<P><font face="Times New Roman, Times, serif" size="5"><b>Feds under fire over Native trust funds</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Evan Kornfeld</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 26, 2001 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE MEDIA are so busy cheerleading George W. Bush's "war on terrorism" that they hardly noticed that Interior Secretary Gale Norton is on trial for contempt of court.</P>
<P>The case involves a class-action lawsuit against the U.S. government for mishandling a trust fund that is supposed to distribute money to 500,000 Native Americans. Norton is accused of providing false evidence to the judge in the case--and destroying e-mails after the judge ordered that they be saved.</P>
<P>The Individual Indian Monies trust fund distributes royalties from grazing, logging, mining and oil drilling on land belonging to Native Americans. Since its start in the 1880s, the fund has been plagued by mismanagement and by sloppy record keeping. It is estimated that the government owes as much as $10 billion in unpaid fees.</P>
<P>The lawsuit against the trust fund--filed by Elouise Cobell, a Blackfeet tribe treasurer--has been going on for six years. In 1999, a federal judge found three Clinton administration officials--Interior Secretary Bruce Babbit, Treasury Secretary Robert Rubin and Assistant Secretary of Interior for Indian Affairs Kevin Gover--in contempt of court after it was revealed that 162 boxes of documents relating to the case had been destroyed.</P>
<P>Since taking office, Norton has dragged her feet in complying with a judge's order to reorganize the trust fund. </P>
<P>Tex Hall, president of the National Congress of American Indians, called this case the Native American equivalent of the Enron scandal. "The only difference is&#133;the government is playing the role of Enron, and this has been going on a lot longer."</P>

