<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Behind the Nazi Le Pen's success</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 26, 2001 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE SUCCESS of Nazi Jean-Marie Le Pen in the first round of France's presidential election last weekend left people around the world in shock.</P>
<P>How could Le Pen--who once called the Holocaust "a mere detail of history," who demands that immigrants be thrown out of France, and whose party contains groups of killer thugs--have gotten more votes than Prime Minister Lionel Jospin of the governing Socialist Party?</P>
<P>How could Le Pen and a rival Nazi candidate together have had a higher vote total than the mainstream conservative candidate, President Jaques Chirac?</P>
<P>The answer has much less to do with mass support for Nazism in France than the broken promises of Jospin and disgust with mainstream French politics.</P>
<P>Five years ago, Jospin won the prime minister's office as part of the "Plural Left" coalition of Socialists, Communists and Greens. These parties were elected in the aftermath of a public-sector general strike against the previous right-wing government's attempts to cut pensions and privatize state-owned companies.</P>
<P>But Jospin mainly pursued the same agenda as his conservative predecessors, privatizing more than the previous five governments combined. So similar were the Jospin and Chirac campaigns that 75 percent of people told pollsters that they saw no difference between the two--who became known as "Chispin." </P>
<P>"They also played into Mr. Le Pen's hands by making crime, one of his favorite subjects, the major topic of their campaigns," the <I>Economist </I>magazine observed. The result was a record abstention rate of 28 percent.</P>
<P>Some Socialist Party leaders blamed far-left parties for "splitting the vote." Yet the Socialists' own "Plural Left" Communist and Green government partners took votes from Jospin as well.</P>
<P>The truth is that by pursuing anti-worker policies in the name of the left, the government allowed Le Pen to portray himself as the spokesperson for what he called "the miners, the steelworkers, workers of all those industries ruined by&#133;Euro-globalization."</P>
<P>The election outcome actually reflected polarization between the right and the left. Left-wing parties got roughly the same share of the vote as they did in 1997. The difference is that two candidates of the revolutionary left got nearly 10 percent of the vote, compared to just over 16 percent for Jospin. And the two revolutionaries outpolled the Communist candidate--a catastrophic result for what was for decades one of the largest parties in France.</P>
<P>Mainstream candidates have urged a vote for Chirac to stop Le Pen in the runoff election. But years ago, Chirac himself held secret meetings with Le Pen to discuss cooperation.</P>
<P>The way to defeat Le Pen and the far right is to build on the anti-Nazi demonstrations seen in every major city on election night.</P>
<P>Plans are underway for a massive May Day demonstration just before the final vote. This kind of activism--plus a renewal of the strikes and protests seen in recent years--can provide an alternative of solidarity to Le Pen's racist scapegoating.</P>
<P>What's more, a struggle in France against racism and for immigrant rights would have an important impact across Europe--where "reformed" fascists are in the government in Italy and far-right politicians are making headway across the continent.</P>
<P>Smashing the threat of Nazism in France means not only fighting the right, but rejecting the discredited politics of Jospin's "Plural Left." That means seizing the opportunity to rebuild a genuinely socialist left--one that fights for the interests of working people.</P>

