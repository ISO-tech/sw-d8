<P><font face="Times New Roman, Times, serif" size="5"><b>Justice for Palestinians!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 26, 2001 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>CHANTING "FREE, free Palestine," some 4,000 protesters gathered outside a Washington, D.C., hotel April 22 where high-level U.S. and Israeli officials had gathered to cheer on Israel's ongoing war.</P>
<P>Inside, the luminaries toasted each other at the annual American Israel Public Affairs Council (AIPAC) meeting. But outside, a spirited protest organized by a broad coalition of groups showed support for the Palestinian struggle for independence.</P>
<P>"What Sharon has done in the Middle East, and the resistance and sacrifices of the Palestinians have brought the demonstrators out," author and veteran antiwar activist Tariq Ali told <I>Socialist Worker</I>. "Even though the U.S. media coverage has been appalling, word has trickled out through the Internet and other alternative sources of information as to what's going on. The fact that this goes on and the U.S. government supports it is now beginning to anger people. They can no longer use September 11 to frighten people and keep them indoors." </P>
<P>The demonstration at the AIPAC conference was part of a wave of pro-Palestinian protests and other actions that took place across the country last week.</P>
<P>At Columbia University in New York City, students organized a walkout April 17, persuading teachers to cancel classes and asking students to turn out in front of the administration building. Some 400 people called on the university to divest from corporations that do business with Israel and demanded an end to Israel's occupation of Palestinian land.</P>
<P>The same day at New York University (NYU), more than 200 people gathered to listen to speakers calling for an end to U.S. aid to Israel. "The occupation has to end," said Sherene Siekaly, a member of Students for Justice in Palestine at NYU. "The last 16 days have been a brutal and inhumane escalation of years of occupation."</P>
<P>Meanwhile, in Austin, Texas, 100 protesters rallied at the state capitol, flying Palestinian flags and demanding an end to Israel's occupation. Like other demonstrations, the event brought out a multiracial crowd that included both Jews and Arabs in support of the Palestinian right to self-determination.</P>
<P>We have to keep building this struggle against Israel's barbaric war.</P>
<I><P>Warren Craig, Laura Durkay, Menejeeh Moradian and Eric Ruder contributed to this report.</P>
