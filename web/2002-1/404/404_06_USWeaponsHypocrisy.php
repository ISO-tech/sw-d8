<P><font face="Times New Roman, Times, serif" size="5"><b>Washington's hypocrisy over weapons</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 26, 2001 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WHAT COUNTRY stockpiles nuclear, chemical and biological weapons, but refuses to allow international inspections of its facilities? George W. Bush and friends want you to believe the answer is Iraq--and that this is the reason to make Iraq the next target in the "war against terrorism."</P>
<P>But there's another country that definitely possesses these weapons, openly flouts international treaties restricting their production, and bars international inspections. The United States of America.</P>
<P>When it comes to "weapons of mass destruction," Washington's hypocrisy knows no limits. Last week, the Bush administration was exposed for trying to unseat Jose Bustani as head of the Organization for the Prohibition of Chemical Weapons, an arms control organization. Apparently, the White House is angry that Bustani's commission wants to examine facilities in the U.S.--just as it examines weapons facilities elsewhere in the world.</P>
<P>The U.S. has given inspectors like Bustani plenty to worry about--like a new "germ factory that could make enough lethal microbes to wipe out entire cities," the <I>New York Times</I> recently reported. Yet hardly a day goes by without some official making another wild accusation about Saddam Hussein's arsenal.</P>
<P>Their evidence is flimsy. Almost no one seriously believes that Iraq has the materials to manufacture nuclear weapons. The regime didn't have all the components to make a nuclear bomb before the Gulf War--and is unlikely to have gotten them since because of UN sanctions.</P>
<P>As for biological and chemical weapons, UN inspectors documented the destruction of 95 percent of Iraq's pre-1990 stockpile--and the UN embargo prevented Iraq from getting the supplies needed to maintain a viable weapon, according to chief UN inspector Scott Ritter. "Qualitatively, Iraq is no longer capable of producing these prohibited goods," Ritter said.</P>
<P>But none of these facts are really the point. Saddam's supposed arsenal of weapons has always been a "phantom threat," in Ritter's words--used by Washington politicians as a justification for its barbaric war.</P>
<P>The latest example of Washington's hypocrisy is the complaint that Saddam won't allow international weapons inspections. But it was the U.S. that ordered Ritter's weapons inspectors to pull out of Iraq--just before another round of saturation bombing in December 1998.</P>
<P>Nor does Washington want inspectors back in Iraq. The White House wants a war, no matter whether Iraq agrees to inspections. As one State Department official said earlier this year, "We won't take yes for an answer."</P>

