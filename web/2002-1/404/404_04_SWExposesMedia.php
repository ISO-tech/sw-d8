<P><font face="Times New Roman, Times, serif" size="5"><b><I>SW</I> exposes the media's lies</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 26, 2001 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Congratulations on 25 years of publishing the best left-wing newspaper in the U.S. It was great to be reminded of some of the first-rate reporting, analysis and agitation you have produced over the past quarter century.</P>
<P>But I have to disagree with one comment by editor Alan Maass. I think <I>Socialist Worker</I> should take pride in being a genuinely objective publication. As Marxist Hal Draper wrote, objectivity is not the same as impartiality.</P>
<I><P>Socialist Worker</I> stands in solidarity with the oppressed and the exploited around the world. In that sense, it rightly does not pretend to be neutral.</P>
<P>But every week, the paper prints the facts that the mainstream media would rather we did not see and exposes the lies and misrepresentations that the ruling class uses to buttress its power. In that sense, <I>Socialist Worker</I> is far more objective than publications like the <I>New York Times</I>.</P>
<P>Keep up the good work. As we fight to rebuild a movement for progressive change in this country, <I>Socialist Worker</I>'s voice of sanity is needed more than ever.</P>
<B><P>Phil Gasper,</B> Berkeley, Calif.</P>

