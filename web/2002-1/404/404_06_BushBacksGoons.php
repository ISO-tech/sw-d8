<P><font face="Times New Roman, Times, serif" size="5"><b>The goons and crooks that Bush backs</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 26, 2001 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. is accepting job applications--for the position of dictator of Iraq.</P>
<P>"Behind closed doors in Washington, in secret diplomatic cables and outside CIA safe houses from suburban Virginia to Kurdistan, the search for solutions--for a plan and a leader--is on," <I>Newsweek </I>magazine commented in March, as Dick Cheney toured the Middle East to build support for a U.S. war on Iraq.</P>
<P>"As the vice president and the Jordanian king talked over a sumptuous meal of seared scallops, grilled beef and berries with mascarpone at the royal palace in Amman, the hard question about overthrowing Saddam was not if, but how and when. And just as vexing: who would replace Saddam?"</P>
<P>The Bush administration has a parade of crooks and goons to choose from. One frontrunner is Ahmed Chalabi, head of the Iraqi National Congress (INC), an umbrella organization of exiled groups operating outside Iraq.</P>
<P>Chalabi, a former banker, is popular with some Pentagon hacks. There is the small matter of the $2 million in U.S. aid that Chalabi is accused of embezzling. According to Britain's <I>Guardian</I> newspaper, the State Department last year threatened to cut off the INC's funding because of "bookkeeping irregularities."</P>
<P>"But by January 30, the doubts were overtaken by patriotic resolve," the paper wrote. "Anyone prepared to fight the Baghdad regime was embraced, and on that very day, Mr. Chalabi had his funding restored."</P>
<P>CBS's <I>60 Minutes</I> reported last month that Chalabi is trying to broker deals "with the president's and vice president's friends in the oil industry, promising executives of both Chevron Texaco and ExxonMobil preferential treatment in a post-Saddam Iraq," the program reported.</P>
<P>The Bush gang is also looking over several Iraqi ex-generals, in the hopes that they might spark a revolt inside Iraq's armed forces. One candidate is Nizer al-Khazraji, the top commander of the Iraqi army from 1980 to 1991. Of course, al-Khazraji did run Iraq's invasion of Kuwait that led to the Gulf War.</P>
<P>Plus, he's currently under investigation by the Danish government for war crimes. He's accused of carrying out the 1988 poison-gas attacks that killed thousands of Kurds in northern Iraq--the very attacks that Dick Cheney repeatedly referred to in insisting that Saddam should be removed from power.</P>
<P>Then there's Gen. Mahdi al-Duleimi, who claimed to <I>Newsweek</I> that his proposal for toppling Saddam has won high marks from the Bush administration. But al-Duleimi, too, is accused of carrying out chemical weapons attacks--as a general during the Iran-Iraq war of the 1980s.</P>
<P>None of these thugs looks very reliable. No wonder <I>National Review</I> editor and veteran Iraq hater Richard Lowry proposed a different solution: U.S. military occupation of Iraq.</P>
<P>"An American occupation would not last years, on the model of a MacArthur regency in Japan," he wrote. "Instead, the U.S. would quickly--say, after less than a year--hand control over to a UN protectorate, with some Arab input to soothe feelings and a non-American--some anodyne European, such as a Swede--running the show. He would in effect act as Iraqi dictator, but without the brace of pistols."</P>

