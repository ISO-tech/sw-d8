<P><font face="Times New Roman, Times, serif" size="5"><b>United out for concessions--again</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 26, 2001 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>UNITED AIRLINES is demanding concessions from unions just weeks after signing a contract granting mechanics a big raise.</P>
<P>"It is in our collective interest to get our costs in line with our revenues," Jack Brace, chief financial officer of United, told the <I>Chicago Tribune</I>. "The labor rates were set in a different time and different revenue environment."</P>
<P>Never mind the fact that the mechanics, members of the International Association of Machinists (IAM), hadn't had a raise since 1994, when they and other union members took massive concessions in exchange for an employee stock ownership program. In fact, the ramp and customer service workers, also IAM members, are still in negotiations for their contract.</P>
<P>Mechanics rejected their initial contract settlement because it mandated future concessions--and won the right to vote on any future givebacks.</P>
<P>United management had planned to settle all labor contracts before pushing for concessions. But pressure from Wall Street forced management's hand.</P>
<P>And even before concessions are put on the table, management is already on the offensive. Many workers who were laid off last fall still have not been recalled, even though United's service has expanded. </P>
<P>Mechanics, for example, are being forced to work a schedule of six days on, two days off--with no overtime and different days off each week. Sig Mueller, manager at United's Airframe Maintenance facility in San Francisco, told mechanics last week that they had to work that schedule indefinitely--and "squeeze a nickel until the buffalo grunts."</P>
<P>In fact, mechanics in San Francisco are turning around planes in half the time it took a few months ago. "When you have layoffs, you have speedup," said Jennifer Biddle, an IAM Local 1781 shop steward in San Francisco. "And management gets big bonuses on cycle time while overtime no longer exists" for mechanics.</P>
<P>Many mechanics are fed up with the IAM and are considering voting for a rival craft union, the Airline Mechanics Fraternal Association (AMFA) to replace their unions. AMFA is no solution. Splitting the IAM will only undermine the solidarity needed among all unions at United to resist concessions.</P>
<P>The fight against givebacks will have to begin now--and continue no matter what the outcome of union elections.</P>

