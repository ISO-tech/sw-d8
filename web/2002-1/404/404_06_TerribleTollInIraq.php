<font face="Times New Roman, Times, serif" size="5"><b>The terrible toll of a decade-old U.S. war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 26, 2001 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>NICOLE COLSON reports on the consequences of the decade-old U.S. war on Iraq--and the nightmare to come if George W. Bush gets away with making Iraq the next target on his "war on terror" hit list.</P>
</B><P>GEORGE W. BUSH is whipping up his "axis of evil" rhetoric again. "A small number of outlaw military regimes today possess and are developing chemical and biological and nuclear weapons," he said in a speech last week at the Virginia Military Institute. "In their threat to peace, in their mad ambitions, in their destructive potential and in the repression of their own people, these regimes constitute an axis of evil, and the world must confront them."</P>
<P>He didn't even have to mention the country by name for everyone to know which one he was really talking about--Iraq. Ever since the U.S. air war on Afghanistan ended, the Bush gang has made it clear that their "war against terror" is headed for Baghdad.</P>
<P>But this will be only the latest phase in a U.S. war on Iraq that has lasted more than a decade. More than 1 million Iraqis are dead as a result. Some were killed by U.S. weapons during the 1991 Gulf War--and the ongoing air war that continues to this day.</P>
<P>Though you almost never read about it in the media, during the last eight months of 2001 alone, U.S. and British pilots fired more than 1,100 missiles at Iraq. Even though there's next to nothing left to bomb. "We're down to the last outhouse," one U.S. official told the <I>Wall Street Journal</I> in 1999.</P>
<P>But even larger numbers of Iraqis are dead because of strict United Nations (UN) sanctions. The most basic goods have been barred from reaching the country--from drugs and medical supplies for hospitals to chlorine and equipment for water purification. The result, according to the United Nations Children's Fund, is that as many as 5,000 Iraqi infants and children die every month as a direct result of sanctions.</P>
<P>As former UN weapons inspector Scott Ritter--not a left-winger, by any means--said in a <I>Salon</I> magazine interview, "We have killed almost six times as many Iraqis trying to eliminate weapons of mass destruction programs than weapons of mass destruction have killed in the entire 20th century."</P>
<P>The U.S. government, along with its loyal British allies, have tried to put a more human face on the embargo--with so-called "smart sanctions" and the oil-for-food formula that allows Iraq to sell some of its oil to pay for imports of food and medicine. But these schemes are a fraud--and ordinary Iraqis continue to pay the price.</P>
<P>Before 1990, Iraq had one of the highest standards of living in the Middle East, with per capita gross national product at more than $3,000. Ten years later, that figure stands at less than $500--and Iraq ranks among the poorest countries in the world.</P>
<P>"What is the Iraq of 2002?" Ritter asked. "It has a pathetic army, a pathetic air force and an economy in tatters, destroyed by misuse, sanctions and the military. Its social infrastructure has been destroyed&#133;This entire 'Iraqi threat' is built on a framework of lies--a house of cards."</P>
<P>But that won't stop Washington's war makers.</P>
<P>They're using the September 11 attacks as an excuse to settle an old score--and topple Saddam Hussein. Even if that means Iraq's nightmare will grow worse.</P>
<P>The war criminals in the White House will stop at nothing to make sure they dominate the oil-rich Middle East. We have to stop them.</P>
<P>Last weekend, more than 75,000 people turned out in Washington, D.C., to say no to the Bush war machine and the horrors it presides over--from Afghanistan to Iraq to Palestine.</P>
<P>We need to build on this turnout--and mobilize the struggle to stop the U.S. war on Iraq.</P>

