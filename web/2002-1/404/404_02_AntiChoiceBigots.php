<P><font face="Times New Roman, Times, serif" size="5"><b>Anti-abortionists' new attack</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 26, 2001 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE ANTI-CHOICE bigots are at it again. On April 17, the U.S. House approved the Child Custody Protection Act for the third time in recent years.</P>
<P>The bill would make it a crime--punishable by up to a year in jail and a $100,000 fine--for anyone other than a parent or legal guardian to take a woman under the age of 17 across state lines to obtain an abortion. That means that a teenager's sister, brother, grandparent or friend could go to jail for helping her to exercise her right to choose.</P>
<P>Far from "protecting" young women against sexual predators--as the bill's sponsors claim--this law would put more lives at risk. The act would take aim at young women living in the 85 percent of U.S. counties where there is no access to abortion.</P>
<P>The legislation's sponsors want to pass off anti-choice bigotry as "concern" for teenage women. But the reality is that the majority of the lawmakers who voted for the bill--including nearly 60 Democrats--don't care about the health and welfare of young women facing unwanted pregnancies. They want to legislate abortion rights away, piece by piece.</P>
<P>The legislation isn't expected to pass the Senate. But this anti-abortion victory in the House is another reminder of the need to keep up the fight to defend abortion rights.</P>

