<font face="Times New Roman, Times, serif" size="5"><b>Can't keep up with the coups</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 26, 2001 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WHEN YOU'RE a world superpower, it's difficult to stay up to date on the coups that you help to orchestrate--especially when they don't succeed.</P>
<P>Maybe that's why there still seems to be some confusion at the U.S. embassy in Caracas, Venezuela, about last week's failed coup against President Hugo Ch&aacute;vez.</P>
<P>On April 12, just hours after the coup, the embassy's Web site listed three official statements. But the pages are blank for "Events in Venezuela: Change in Government" and "U.S.-Spain Joint Statement on the Situation in Venezuela."</P>
<P>Clicking on the link "Statement by the Ambassador of the United States (Spanish)" brings up an October 15 press release on "Humanitarian Aid to the Afghan People"--but nothing about the coup.</P>
<P>Fortunately, Ambassador Charles Shapiro's statement is available in the Spanish version. Without mentioning the temporary removal of elected President Hugo Ch&aacute;vez, Shapiro praises the interim government's pledge to investigate the former government's apparent shooting of demonstrators.</P>
<P>Shapiro goes on to praise the "new government" for its "intention to strengthen democratic institutions and processes within a framework of respect for human and civil rights."</P>
<P>Of course, the coup makers' choice for president, businessman Pedro Carmona, suspended the constitution and abolished the National Assembly.</P>
<P>How's that for "strengthening democratic institutions"?</P>
<P>--<I>Washington Post,</I> April 19, 2002</P>

