<p><font face="Times New Roman, Times, serif" size="5">The mouths of babes</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CALIFORNIA Gov. Gray Davis got a bit of a shock when he stopped to chat with fourth graders on a field trip to the State Capitol building in Sacramento recently.</P>
<P>When Davis asked the kids what they wanted to do when they were older, he got the usual responses--until one of the 10-year-olds informed the governor, "I want to overthrow the government." Davis was a bit taken aback.</P>
<P>The 10-year-old turned out to be Kathlyn Elizabeth Bening Beatty, daughter of actors Annette Bening and Warren Beatty. After flirting with the U.S. presidential campaign in 2000, Beatty is rumored to be considering a run for California governor in 2006.</P>
<P>--<I>Washington Post,</I> April 19, 2002</P>


