<p><a name="Verizon"></a><font face="Times New Roman, Times, serif" size="4"><b>Verizon</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Amy Muldoon, CWA Local 1106</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Members of the executive board of Communication Workers of America (CWA) Local 1106 got the surprise of their lives at the last general membership meeting on April 16.</P>
<P>For years, the board has talked about membership as though we are passive and stupid, while they've coasted toward retirement. But on April 16, more than 300 members packed the hall to vote on a proposed dues increase--a full 50 percent more members than attended the last general membership meeting.</P>
<P>This increased mobilization was a result of a new coalition in the local that spans multiple garages and inside facilities. Many members feel the board is out of touch with the membership--especially among younger and African American members.</P>
<P>In fact, the board is unapologetic about the fact that nine out of its 10 members have 32 years in the company, and 10 out of 10 are white--despite the fact that 46 percent of the union's members are African American. The board's cocky attitude toward the members, however, is not matched by its passive stance toward the company.</P>
<P>Verizon implemented a speed-up that links productivity to disciplinary action for the first time. While the board has talked tough against this, only limited action has been taken. This was made crystal clear in the last general membership meeting when President Tony Caudullo talked about threatening management with a walkout--then refused to hear a motion to take a strike vote. Caudullo called the motion "inappropriate" and moved the agenda before anyone could respond.</P>
<P>But on April 16, Caudullo couldn't just move on. When the vote for the dues increase finally came up, the vote had to be taken twice, because the first "count" consisted of Caudullo simply saying "it passed"--without counting a single hand.</P>
<P>In the second vote, the dues increase did pass, by a couple of dozen votes. This close vote represents a step in the right direction for rank-and-file members, and has given coalition members a sense that we are having an effect. </P>
<P>The day after the vote, a white district steward at one garage felt compelled to ask Black members--in perhaps the first time the union has officially reached out to non-white members at the garage--if they felt they were being represented well.</P>
<P>With a strike on the horizon for 2003 and board elections in November, this is just the start.</P>

