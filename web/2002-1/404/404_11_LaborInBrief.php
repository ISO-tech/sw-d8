<p><a name="GHI"></a><font face="Times New Roman, Times, serif" size="4"><b>Group Health Insurance</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Danny Katch</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Employees at Group Health Insurance (GHI) process claims for thousands of public-sector workers in New York state every day. Now, they're fighting for their own health insurance.</P>
<P>This week, hundreds of GHI workers--members of Office and Professional Employees Local 153--hit the picket lines more than a month after their contract expired. "We'd rather be working," said Tanzeel Shibzada, "but our hands have been tied. We're being treated like crap."</P>
<P>Previously, a benefit of working for the insurance giant was that workers didn't have to pay co-payments or premiums. Now, however, GHI is demanding a $10 co-pay for medical visits and $20 for prescriptions.</P>
<P>Workers are striking against this corporate greed. "We're a nonprofit, but they're getting big money--company cars, health club memberships," Christie, a Medicare claims approver for 14 years, told <I>Socialist Worker</I>. "Why not give that money to us? We are their business."</P>
<P>Workers are gearing up for a tough battle. GHI has prepared for the strike by hiring temporary workers and bringing a veteran health care union buster onto their negotiating team.</P>
<P>But strikers potentially have weapons of their own. Most of GHI's clients are union workers: teachers, firefighters and transit workers. The New York City Central Labor Council needs to let GHI bosses know that city workers won't tolerate their claims being handled by scabs and temps.</P>

