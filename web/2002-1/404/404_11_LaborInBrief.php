<p><font face="Times New Roman, Times, serif" size="4"><b>LA teachers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Karl Swinehart, United Teachers Los Angeles</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LOS ANGELES--Elections for the leadership of United Teachers Los Angeles (UTLA) came to a close on April 9, with John Perez winning the presidency. Perez won in a runoff election that had record-low participation, with less than 25 percent of the union's 40,000 members voting. </P>
<P>The runoff was a result of a split in the radical vote in the union. If combined, the votes for the more militant candidates Warren Fletcher and Mike Cherry would have won the initial election.</P>
<P>However, there was no unified strategy around the election by those teachers who felt dissatisfied with the last settlement. As a result of this split, the two more conservative candidates--Becki Robinson and Perez--went to the runoff.</P>
<P>The change in leadership comes during budget cuts and attacks on union rights from both the district and the state. The district is proposing class size increases, cuts in counselors and nurses, and a six-year wage freeze.</P>
<P>Under the guise of auditing low-performing schools, the district and state are asking teachers to sign "loyalty oaths" and are threatening to relocate faculty.</P>
<P>Although the low voter turnout reflects an inactive union membership, there are signs that this may be turning around. UTLA has begun to mobilize teachers to pack school board meetings in order to show our opposition to salary freezes and budget cuts. </P>
<P>In addition, UTLA members at 10 schools that are currently being audited by the state are organizing informational pickets to expose the hypocrisy of the budget-slashing bureaucrats who scapegoat teachers. </P>
<P>Job actions and mobilizations like these are steps in the right direction. We need more of this kind of activism. </P>

