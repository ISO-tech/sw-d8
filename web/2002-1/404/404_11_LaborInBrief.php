<p><a name="Windows"></a><font face="Times New Roman, Times, serif" size="4"><b>International Windows Company</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Federico Moreno</b></font> </P>
<font face="Times New Roman, Times, serif" size="3"><P>LOS ANGELES--More than 150 workers from International Windows Company have been abandoned by their union, Teamsters Local 986, following a six-month strike. Many strikers had been with the company for more than 25 years and believe that the company and union collaborated to "clean house" of older workers slated for retirement.</P>
<P>Just prior to Local 986 elections, the union endorsed a September 10 strike over low wages and poor benefits. The strike began successfully, but union leaders soon pulled a disappearing act. "We had no representation," Rafael Ramos told <I>Socialist Worker</I>. </P>
<P>In six months, the only offer that the company made was for workers to return to work--with lower wages and no vacations! Then, on March 18, Local 986 informed workers that they had come to an "unconditional agreement" and were cutting off workers' strike benefits.</P>
<P>"Where's our rights?" Ramos asked. "If you complain, you're out on the streets like us. They simply told us to pick up our last check. When we did, 10 armed policemen were guarding the Local's gate."</P>
<P>The workers are currently seeking legal representation in order to help them fight back against this assault.</P>

