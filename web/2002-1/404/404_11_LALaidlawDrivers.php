<font face="Times New Roman, Times, serif" size="5"><b>LA school bus drivers strike</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By William Figueroa and David Rapkin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 26, 2001 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LOS ANGELES--School bus drivers here are in their fourth week of a bitter strike against Laidlaw Education Services, which provides half the bus service for the Los Angeles Unified School District (LAUSD). The 842 strikers, members of Teamsters Local 572, are fighting for a living wage and decent health and pension benefits.</P>
<P>Laidlaw bus drivers start at $8.25 an hour, pay 60 percent of their own health benefits (no dependents allowed), and retire with a pension of $65 per month after 22 years of service! With those wages and miserable benefits, the predominantly Black and Latino drivers often have to take a second job just to keep themselves above the poverty line.</P>
<P>Meanwhile, LAUSD employs its own team of district drivers, who make an average of $8.50 more per hour than Laidlaw drivers. And while district drivers top out at $25 per hour, Laidlaw drivers can make a maximum of just $14.25 per hour.</P>
<P>It's an arrangement that Don Owens, spokesperson for Local 572, calls "criminal." According to Owens, 60 percent of the drivers are single moms who can't afford the company's health care "benefits." "Most of our members resort to welfare for medical care," Owens told <I>Socialist Worker</I>. "They spend all day taking care of children, and don't get paid enough to care for their own kids."</P>
<P>On the picket line, driver Tammy Sum told <I>Socialist Worker</I> that, in addition to a living wage, strikers are demanding respect. Sum described Laidlaw supervisors, who "treat drivers like we're low class and ignorant," instead of "acknowledging that we're hard-working, responsible adults who transport and care for hundreds of children everyday."</P>
<P>When Sum recently needed a signature from a Laidlaw yard manager to keep her child in subsidized day care, he told her that since she was on strike, her kids "could find their own day care."</P>
<P>"He's intimidated by us since we're taking a stand," she said. </P>
<P>Laidlaw is a multibillion-dollar, Canada-based transportation company that can underbid its competitors because it pays its workers poverty-level wages.</P>
<P>Last year alone, Laidlaw's U.S. profits were $1.5 billion; in Canada, they netted $2.2 billion. But it refuses to give workers full health benefits and modest five percent raises for three years. </P>
<P>Last week, Laidlaw insulted drivers by delivering their final paychecks with a written "offer" that slandered the union and turned out to be the exact offer members had overwhelmingly rejected in February. What's more, according to Owens, 52 percent of the paychecks were short between $10 and $40.</P>
<P>While LAUSD claims to be neutral in the dispute, schools superintendent Roy Romer said last week that he thinks "Laidlaw is paying its workers a living wage." Picketers are still laughing trying to imagine Romer--the former governor of Colorado--living on $8.25 an hour.</P>
<P>As <I>Socialist Worker</I> went to press, drivers were still waiting for a serious offer from the company, after having voted down the company's previous offer 412 to 2. In the meantime, drivers show no sign of backing down.</P>
<P>"Sometimes you have to fight for what you need," said Sum. "If you respect yourself and your coworkers, the sky's the limit."</P>

