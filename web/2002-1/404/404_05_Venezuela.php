<P><font face="Arial, Helvetica, sans-serif" size="2"><b>VENEZUELA</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Will U.S. role in the coup be exposed?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 26, 2001 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH administration was scrambling last week to counter accusations that it was a "partner" in the coup that briefly toppled Venezuelan leader Hugo Ch&aacute;vez Frias.</P>
<P>After a big antigovernment demonstration that ended in gunfire earlier this month, Ch&aacute;vez was arrested, and Pedro Carmona, the head of country's employers' federation was sworn in. Carmona promptly suspended the National Assembly and fired the justices of the Supreme Court--stoking the anger of Venezuelan workers and the poor, as they took to the streets to protest the coup.</P>
<P>In less than 48 hours, Carmona was forced to resign, and Ch&aacute;vez returned in triumph.</P>
<P>As soon as Ch&aacute;vez was ousted, the Bush administration was celebrating. White House spokesperson Ari Fleischer dismissed the idea that Ch&aacute;vez had been overthrown--and argued that he had "lost his job" because he opposed democracy.</P>
<P>Afterward, the administration blamed the story put out by the Venezuelan media that Ch&aacute;vez had resigned to explain why it had welcomed the toppling of a democratically elected leader. No other government in the Western hemisphere was fooled. Even close U.S. allies like Mexico opposed Ch&aacute;vez's ouster.</P>
<P>In fact, the U.S. has been ratcheting up pressure on Ch&aacute;vez for some time. Earlier this year, a string of administration officials--including Secretary of State Colin Powell and CIA Director George Tenet--warned of a "climate of crisis" in Venezuela, prompting stories wondering if Washington was preparing to back action against Ch&aacute;vez.</P>
<P>Nor were the coup makers shy about seeking support from the Bush gang in recent months, trooping to the U.S. embassy in Caracas and to Washington in "a stream," as the <I>New York Times</I> put it. As one Pentagon official admitted, "We were not discouraging people."</P>
<P>That's no surprise. The Bush administration is packed with veterans of the U.S. government's dirty wars in Latin America under Ronald Reagan and George Bush Sr. In fact, the State Department's point man for Venezuela--who met regularly with Ch&aacute;vez opponents in the weeks before the coup--is none other than Otto Reich.</P>
<P>Now the assistant secretary for Western hemisphere affairs, Reich is a Castro-hating Cuban American who is friends with some of the vilest terrorists in the right-wing Cuban exile community. Under Reagan, Reich was a player in the war on Nicaragua's left-wing government, conducted by Oliver North's "basement White House."</P>
<P>And guess who's in charge of Latin America for the Pentagon today? Rogelio Pardo-Maurer, who--as chief of staff for the Contras' representative in Washington--worked directly with Reich's State Department lie machine.</P>
<P>The idea that these goons didn't know what was in the works is laughable. Ch&aacute;vez has been a target since the Bush administration took over. The Texas oil boys who run the White House want a stable regime in Venezuela--the third-largest supplier of oil to the U.S.</P>
<P>And Ch&aacute;vez, while falling short of his radical rhetoric in terms of policies to redistribute the country's oil wealth, is a sharp critic of U.S. foreign policy.</P>
<P>You don't need a smoking gun to know that the Bush administration wanted this coup--despite their talk about democracy.</P>

