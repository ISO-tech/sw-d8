<P><font face="Times New Roman, Times, serif" size="5"><b>City University of New York</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Afsaneh Moradian</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 26, 2001 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--The battle over budget cuts to the City University of New York (CUNY) system was heating up as <I>Socialist Worker</I> went to press.</P>
<P>Students, faculty and community members were planning a demonstration and march to City Hall on April 30, to demand that New York Gov. George Pataki and New York City Mayor Michael Bloomberg drop proposed budget cuts to CUNY students. </P>
<P>Students are being hit with a whopping $155 million cut to the Tuition Assistance Program, a $12 million cut to scholarships, an end to the last semester free and a $75 "technology fee." And, in a vicious racist attack, tuition has been doubled for undocumented students. </P>
<P>CUNY professors have begun to cancel classes in support of the protest, and high schools are also organizing for the demonstration. </P>
<P>Come out on April 30 and tell politicians that we will fight to defend our right to an affordable education!</P>
