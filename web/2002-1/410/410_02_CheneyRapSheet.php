<P><font face="Times New Roman, Times, serif" size="4">Financial shenanigans while he ran Halliburton</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Cheney's corporate rap sheet</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 7, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SPREADING WAVES from the Enron-Arthur Andersen scandal are lapping at the feet of White House puppet master Dick Cheney.</P>
<P>Last week, the <I>New York Times</I> revealed that Halliburton--the oil services company where Cheney was CEO before signing on as the power broker behind George W. Bush's throne--is under investigation by the Security and Exchange Commission for financial shenanigans. Seems that during Cheney's final few years at the company, Halliburton began reporting profits that it hadn't actually earned yet.</P>
<P>Companies like Halliburton that run big construction projects thrive on cost overruns--the extra cash that they get from customers like the Pentagon beyond what they bid to do the job. Beginning in 1998, Halliburton started to estimate how much extra it would ultimately be paid in overruns, reporting the figure as income on its statements. The accounting pick-me-up boosted the company's bottom line by $100 million.</P>
<P>Guess which accounting firm audited Halliburton's cooked books? Arthur Andersen, of course.</P>
<P>Naturally, Cheney insists now that he knew nothing about how the company's bean counters did their job. But at the time, he didn't mind taking credit for the company's seemingly excellent financial performance--to the tune of $36 million in salary, bonuses, benefits and other compensation in 2000.</P>
<P>And he sure seemed enthusiastic about Arthur Andersen's creative accounting schemes in a promotional video for the firm. "I get good advice, if you will, from their people based upon how we're doing business and how we're operating--over and above just the sort of normal by-the-books auditing arrangement," Cheney said.</P>
<P>Over and above, indeed! Cheney made out like a bandit, thanks to the shell game that his accountants and auditors were playing.</P>
<P>Now Cheney's new "boss," George W. Bush, responding to the Enron scandal, says that CEOs should lose bonuses for "grossly inaccurate" financial statements.</P>
<P>When will Cheney start forking over his millions?</P>

