<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Why the two-state solution won't work</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 7, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE FIGHT for Palestinian liberation is often put in terms of "ending the occupation." The implication is that the main obstacle to peace is Israel's occupation of the West Bank and Gaza, which began in 1967. If Israel would withdraw to this pre-1967 border, the argument goes, it would clear the way for a Palestinian state.</P>
<P>This proposal is known as the "two-state solution"--and for the last decade, it was the basis for talks between Israel and the Palestinians. But Israel's military offensive in recent months--aimed at destroying the institutions and infrastructure of the Palestinian Authority--demonstrated the problem with the two-state solution.</P>
<P>Israel's rulers won't stand for it. In May, the central committee of Prime Minister Ariel Sharon's right-wing Likud Party passed a resolution that rejected any future Palestinian state in the West Bank or Gaza.</P>
<P>And during the 1990s, even as it talked peace, Israel's other main party--the Labor Party--oversaw stepped-up construction of Israeli settlements in areas that were supposed to become part of the new Palestinian state.</P>
<P>The settlements--mini-fortresses whose residents are the most fanatically committed to Israel's colonial project--were a way to create "facts on the ground" and make a pullback to the 1967 borders an ever more remote possibility.</P>
<P>But even if Israel were somehow to accept a two-state solution, there's a more fundamental problem. A Palestinian statelet in the West Bank and Gaza--which amount to just 22 percent of historic Palestine--is a recipe for further conflict, not peace.</P>
<P>First, these two territories are disconnected--meaning that Israel could shut down travel between them on the slightest whim. Second, these territories have less fertile land and less access to water. Third, the West Bank and Gaza are in a state of economic collapse, having faced years of decline under Israeli domination.</P>
<P>An impoverished state on two slivers of land would find itself in a constant confrontation over resources. And that's assuming that Israel, with the fourth or fifth most powerful military in the world, would put aside its expansionist designs.</P>
<P>But the most serious problem with the two-state solution is that it does nothing to address the grievances of the more than 700,000 Palestinians who were forced to flee their homes when Israel took Palestine by force in 1948. Today, the descendants of these people--who now number more than 1 million--still live in refugee camps dotting the region.</P>
<P>The right of return for Palestinian refugees--a right that is guaranteed under international law--makes the whole idea of two ethnically pure states unworkable. That's why the only way to accomplish a just settlement in the Middle East is to have a secular, democratic state in all of Palestine--where Jews, Arabs, Christians and Muslims would all enjoy equal rights.</P>
<P>For years, this was the project of the Palestine Liberation Organization. But after decades of defeats at the hands of Israel's barbaric military, Yasser Arafat's Fatah faction retreated--adopting the two-state solution, despite all its obvious flaws.</P>
<P>The problem isn't that Arafat hasn't conceded enough--as Israel's supporters repeat over and over. The problem is that the two-state solution Arafat now supports concedes too much--because it accepts the idea of ethnic partition.</P>
<P>The real obstacle to peace and justice in the Middle East today is the Zionist vision of an exclusively Jewish state. This vision both drives Israel to expand its territorial control--and its hopes of ridding the land it occupies of Palestinians.</P>
<P>Critics of the goal of a secular, democratic state in all of Palestine say that it's an impractical dream. But it's far more impractical to believe that a just peace could be achieved by accepting the heart of Israel's colonial project.</P>
<P>Jews, Christians and Muslims lived together in Palestine for centuries in relative harmony until the Zionist movement sought the support of Western imperialism to establish the state of Israel.</P>
<P>There will never be a future peace without justice--and that means a single secular state in Palestine.</P>

