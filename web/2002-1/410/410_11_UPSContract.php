<P><font face="Times New Roman, Times, serif" size="5"><b>United Parcel Service</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Donny Schraffenberger, steward, Teamsters Local 705</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 7, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AFTER 93 percent of Teamsters at United Parcel Service (UPS) voted to authorize a strike last month, management has finally started to move on contract negotiations.</P>
<P>A tentative deal has been reached, according to Parcel Director Ken Hall, on a stronger penalty when supervisors do Teamsters work. This is a major problem at UPS, because when supervisors do our work, they're stealing time, money and potential jobs from union members.</P>
<P>A national Day of Action is set at UPS hubs across the country for June 4. The event will kick off a weeklong petition drive that calls for support of the Teamsters National Negotiating Committee. The goal is to sign up every UPS Teamster to show the company we're united for a strong contract.</P>
<P>Although this show of unity is a good step, members need regular meetings to discuss updates in the contract talks, because many of them have serious questions about negotiations and a possible strike. More plant-gate rallies would also help build the solidarity needed to beat UPS. For too long, members have been kept in the dark, and this has only aggravated the rumor mill.</P>
<P>The issues being discussed during negotiations this week in Washington will include excessive and forced overtime. Instead of package car drivers working long hours while part-timers starve trying to get more minutes on the clock, ending forced overtime could help create more full-time jobs.</P>
<P>Also on the table is job security. Recently, UPS has laid off full-timers while it hires more and more nonunion temp agency workers. Teamsters should do these jobs.</P>
<P>Remember--UPS has had five record-breaking profit years since 1997. They have the money to give us what we want, but we have to do "whatever it takes," including a national strike to force them to give into our just demands.</P>

