<P><font face="Times New Roman, Times, serif" size="4">Talk of "reform" hides a power grab</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Ashcroft unleashes the FBI</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By John Butell and Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 7, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE FEDERAL government's security and law enforcement apparatus is using the controversy over September 11 to grab frightening new powers that will be used to squash dissent.</P>
<P>The feds have been on the hot seat for missing warnings that the September 11 hijackings were being planned. At a press conference and in TV interviews last week, FBI Director Robert Mueller owned up to accusations from his own agents about the bureau's pre-9/11 blunders.</P>
<P>His latest statements contradict much of what he said publicly after September 11--amounting to an admission that Mueller helped the Bush White House to cover up what it knew about the hijackings.</P>
<P>But Mueller's "mea culpa" came alongside the announcement of a reorganization of the FBI to focus on the "war on terrorism." In reality, the feds are getting broad new powers to spy on political and religious organizations.</P>
<P>Under the new guidelines, local field offices will no longer need permission before beginning counter-terrorism investigations. The probes can continue for up to a year before being reviewed, and agents won't have to show probable cause that a crime is being committed or planned before they begin.</P>
<P>Rules restricting the surveillance of groups meeting in religious buildings, such as mosques and churches, have been scrapped. And the bureau is now free to search public databases and the Internet, for example, monitoring Web sites and online chat rooms, without restrictions.</P>
<P>In short, the feds have a green light to spy on anyone they want--in the name of the "war on terrorism."</P>
<P>"The FBI is now telling the American people, 'You no longer have to do anything unlawful in order to get that knock on the door,'" said Laura Murphy, director of the American Civil Liberties Union's national office. "You can be doing a perfectly legal activity, like worshipping or talking in a chat room, and they can spy on you anyway."</P>
<P>It's obvious who the first victims of the FBI's new powers will be--the same ones who suffered the brunt of Attorney General John Ashcroft's witch-hunt after September 11. As many 2,000 people--most of them young men of Arab descent--were detained as part of the September 11 investigation, and hundreds remain behind bars. Yet not a single one has been charged with any crime remotely connected to terrorism.</P>
<P>The truth is that more power for Ashcroft and the feds will make our lives less secure, not more--especially if you happen to be a person of color. The Bush gang wants to use September 11 to restore the FBI's power to suppress political dissent.</P>
<P>In fact, Ashcroft's new guidelines remove virtually all of the restrictions placed on the FBI in the 1970s--after outrage at the bureau's targeting of political activists forced Washington to limit its powers. Washington wants to return to the days of COINTELPRO--the FBI's espionage campaign against the civil rights, antiwar and other movements of the 1960s.</P>
<P>The FBI mounted extensive operations against such leaders of those struggles as Martin Luther King. But in the end, Washington's police-state tactics were pushed back--by a movement that turned popular opinion against the belief that we should give up our rights in the name of security.</P>
<P>Make no mistake: Today's assault on civil liberties is designed to silence political opposition. We have to organize to show Ashcroft and the rest of the Bush gang that the resistance to their war--at home and abroad--has just begun.</P>

