<P><font face="Times New Roman, Times, serif" size="4">Company agrees to arbitration after strike threat</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Workers at Verizon take to New York's streets</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By a member of CWA Local 1109</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 7, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Thousands of Communications Workers of America (CWA) members at Verizon came out in force May 29 to let the phone giant know that any attempt to lay off workers will be met with stiff resistance. </P>
<P>Manhattan's 6th Avenue was a sea of red shirts stretching for three blocks with signs reading, "Save $40 million, lay off Seidenberg and Babbio"--two of Verizon's top executives. </P>
<P>"We should find out where Seidenberg lives and picket every day," a technician from Brooklyn told <I>Socialist Worker</I>. "We should also picket Verizon Wireless. That's their big money maker. Find their most profitable areas and picket there."</P>
<P>"Not one job" and "Seidenberg sucks" were the most popular chants, along with "One layoff, no work." The rowdy rush-hour rally merited three police helicopters and plenty of cops, but received almost no press. </P>
<P>Verizon workers across the state face possible layoffs due to a declared surplus of 1,718 jobs. The company claimed that an "external event" had forced the cuts, giving them the justification to fire the least senior workers. Verizon cited September 11 and its economic aftermath, but then refused to arbitrate the issue until CWA called a May 31 deadline to arbitrate or face a statewide strike. Finally, the company caved.</P>
<P>If the arbitrators rule in Verizon's favor, the company will be able to get rid of union workers once buyouts and retirements are exhausted. Once Verizon has kicked open the door to layoffs--which have never occurred in New York--then more and more layoffs will follow. </P>
<P>And while Verizon is crying poverty to the arbitrators, it's telling Wall Street another story. Moody's Investors Service recently gave Verizon its Prime-1 rating for increasing cash flow and paying down long-term debt.</P>
<P>Bad investments by upper management and overcapacity in the telecommunications industry were what brought the company's profitability into question. Why should workers pay for their mistakes?</P>
<P>If Verizon wins the right to lay off workers, the union then faces a real challenge. Rank-and-file workers must keep the pressure on using the current tactics.</P>
<P>--Work safely by the company's rules.</P>
<P>--Continue the grievance campaign, especially against the Service Excellence Plan.</P>
<P>--Document and publicize the terrible state of telephone facilities.</P>
<P>In addition, pickets at Verizon Wireless, Verizon-sponsored events and executive's houses would effectively pressure the company. Job actions that involve all the membership would also send a clear message that the union is united and ready for a serious fight that will set the stage for the 2003 contract battle. </P>
<P>The will, creativity and energy necessary to stop Verizon will come from the membership, but only if organized.</P>
