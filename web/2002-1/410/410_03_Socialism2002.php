<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">A socialist world is possible</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Fighting for the future</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 7, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>EVERY DAY brings new examples of the brutality and humiliations of the society we live in.</P>
<P>George W. Bush's "war on terrorism" is punishing the poorest nations in the world, like Afghanistan, and stoking conflict elsewhere--for example, in India and Pakistan. Meanwhile, Dubya and his corporate buddies are waging war on U.S. workers--with pink slips, attacks on the right to organize and the shredding of the social safety net.</P>
<P>None of this is the sole territory of Bush, of course. If there's anything that the world's rulers can agree upon, it's protecting the wealth of a select few at the expense of the majority. That's how it's possible for millions of people to go hungry while tons of grain go unused simply because distributing it wouldn't be profitable.</P>
<P>For many people, the details of how unjustly society works aren't new. But when it comes down to it, the real question is whether we have the power to do anything about it.</P>
<P>The media do their best to ignore examples of working people standing up against injustice--whether it's the 100,000 people who marched against Bush's war on April 20, or the more than 1 million who poured into the streets of France to challenge the threat of Nazism, or the masses of Argentinians who rebelled against the misery of the free market. Likewise, ordinary people rarely show up in the history books.</P>
<P>That's why socialists seize on every opportunity to celebrate today's struggles--and the rich tradition of the socialist movement, both in the U.S. and around the world. These struggles, past and present, show why--and how--a better world is possible.</P>
<P>And they show why revolutionary organization is key. Our rulers are organized. We have to be, too. That's why <I>Socialist Worker</I> is cosponsoring Socialism 2002 in Chicago on the weekend of June 13-16.</P>
<P>The conference will feature dozens of meetings, involving activists from struggles around the world, as well as the U.S.--all discussing the next step in our fight, from the opposition to Bush's war to the struggle for global justice.</P>
<P>At the same time, Socialism 2002 will focus on the tools that we need for the wider effort to put forward a socialist alternative. That means learning the experiences our revolutionary history, from the Bolshevik Party during Russian Revolution to the battle for Chile in the 1970s, for example.</P>
<P>And it means discussing the political ideas that have inspired and guided the workers' movement, from Marx's analysis of the capitalist economy to Leon Trotsky's theory of permanent revolution.</P>
<P>The need for a better world is obvious. The question that needs to be answered is how to get there.</P>

