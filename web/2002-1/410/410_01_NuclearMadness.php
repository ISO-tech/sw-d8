<font face="Times New Roman, Times, serif" size="5"><b>A world armed to the teeth</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 7, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ANYONE IN their right mind is terrified by the threat of a war between India and Pakistan. For the past two weeks, the two regional rivals have been at the brink of a conflict that could all too easily lead to a catastrophic exchange of nuclear weapons.</P>
<P>"American intelligence estimates put the toll in the event of a full exchange of the two nuclear arsenals at 12 million dead with maybe 7 million wounded--an instant slaughter unprecedented in the history of mankind," reported Britain's <I>Guardian</I> newspaper.</P>
<P>In Washington, George W. Bush added his voice to the chorus of world leaders calling for India and Pakistan to show "restraint." But even as Bush sent Defense Secretary Donald Rumsfeld to South Asia to reign in the U.S. government's quarreling "war on terrorism" allies, administration officials revealed that the Energy Department was restarting production of plutonium triggers--for use in U.S. nuclear warheads.</P>
<P>Apparently, some nukes are good nukes--as long as the U.S. is doing the threatening.</P>
<P>Isn't this the same Bush who just traveled to Russia to sign an arms control treaty with Russian President Vladimir Putin?</P>
<P>In reality, the treaty is a worthless scrap of paper. And within days of his return, Bush was talking tough--telling cadets at West Point that that the U.S. has to be ready to "take the battle to the enemy" with pre-emptive strikes.</P>
<P>Washington has some nerve lecturing the world about "restraint" and "peace." After all, the U.S. remains the only country to ever use nuclear weapons--the atomic bombs dropped on Hiroshima and Nagasaki at the end of the Second World War.</P>
<P>Then there's the Pentagon's "nuclear hit list" that the Bush administration leaked to the media earlier this year--a blueprint for U.S. nuclear attacks against seven "rogue nations." Or the scheme to build "mini-nukes," which administration hawks want to use in a "limited" nuclear war.</P>
<P>Next year, Bush plans to spend well over $1 billion <I>every single day</I> on the Pentagon. But when it comes to a prescription drug benefit for the elderly or an increase in the minimum wage, the money just isn't there, he claims.</P>
<P>Worldwide, the same twisted priorities are clear. Since the Second World War, governments around the globe have spent close to $10 trillion on defense. Yet every day, tens of thousands of people die from famine, disease or other preventable causes. </P>
<P>The money spent each year arming the world's governments to the teeth could feed the entire planet. We say no to these sick priorities--and we'll fight for a different world, where war and poverty are ended forever.</P>

