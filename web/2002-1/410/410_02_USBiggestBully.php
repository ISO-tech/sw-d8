<P><font face="Times New Roman, Times, serif" size="5"><b>Biggest human rights bully on the block</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 7, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. government is one of the biggest human rights bullies on the block, according to Amnesty International's 2002 annual report.</P>
<P>Amnesty documented a series of significant human rights violations, including Washington's authorization of military tribunals to prosecute supposed "terrorists"; its refusal to abide by the Geneva convention in its treatment of detainees being held in Guant&aacute;namo Bay, Cuba; and the indefinite detention of hundreds of Muslim and Arab immigrants in U.S. prisons following September 11.</P>
<P>"There has always been some excuse for why human rights needs to take second place or even third or fourth place to other concerns," said Amnesty's U.S. executive director William Schulz. "Today the excuse is national security."</P>
<P>But Amnesty also challenged the continued use of the barbaric death penalty in the U.S. and Washington's sales of arms and chemical weapons to countries with terrible human rights records.</P>

