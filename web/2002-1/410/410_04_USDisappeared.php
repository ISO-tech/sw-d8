<P><font face="Times New Roman, Times, serif" size="4">Indefinite detention prompts a hunger strike</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"Disappeared" in the U.S.</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 7, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>The U.S. government is taking a page out of the Israeli government's book when it comes to the brutal detentions of Palestinians. Jamal Saleem, a Palestinian who has been held for the last eight months at the Passaic County Jail in Patterson, N.J., has gone on a hunger strike in an attempt to win humane treatment and justice from the Immigration and Naturalization Service (INS).</P>
<P>The 40-year-old Saleem was arrested in October for overstaying his visa. In November, he was ordered deported--but was told that he couldn't be deported since his country was at war. The INS "solution" has been to hold him indefinitely. </P>
<P>Saleem's family in Palestine has had no income without him. Meanwhile, he is in excruciating pain from a degenerative back ailment that his jailers refuse to treat.</P>
<P>He began his hunger strike on May 9, demanding that he be brought before a judge so that he can either be released or offered bail; that he receive medical treatment for his back ailment; and that the plight of his family--deprived of its sole breadwinner--be considered in his case.</P>
<P>In addition, Saleem has declared that his hunger strike is a protest against the detention of hundreds of Arab, Muslim and South Asian immigrants without due process, access to a lawyer, proper medical care and under conditions of verbal and physical abuse.</P>
<P>The day after he began his hunger strike, Saleem was given a court date and received treatment for his back. But when he was brought to the INS building in Newark, an INS agent tried to force-feed him. Saleem refused. He was returned immediately to jail and has yet to receive a new hearing. </P>
<B><P>Wade Savitt,</B> New York City</P>
