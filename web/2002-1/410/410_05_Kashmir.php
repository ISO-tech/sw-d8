<P><font face="Arial, Helvetica, sans-serif" size="2"><b>INDIA/PAKISTAN</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Washington's "war on terrorism" set the stage for this latest conflict</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The drive to nuclear war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Ganesh Lal</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 7, 2002 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>INDIA AND Pakistan remained at the brink of war at the beginning of June after two weeks of hard-line rhetoric from political leaders and regular exchanges of artillery fire in the disputed state of Kashmir.</P>
<P>On the strength of statements by Pakistan's President Pervez Musharraf and India's Prime Minister Atal Behari Vajpayee that neither would start a nuclear war, commentators claimed that the two sides were stepping back.</P>
<P>But the situation remains frightfully tense--with more than 1 million troops massed along the Line of Control that splits Kashmir between an Indian-occupied area and a Pakistan-occupied one.</P>
<P>Even a conventional war between the two countries would be devastating. But the threat of a nuclear exchange is real--with estimates of casualties as high as 15 million.</P>
<P>The U.S. media tend to portray the source of the conflict as religious--a battle between India's Hindu fundamentalist government and Pakistan's Islamists. In fact, the crisis is a part of the power struggle in which the U.S. has meddled on both sides.</P>
<P>The Indian government claims that it's merely following the lead of the U.S. in fighting a "war against terrorism." It holds Pakistan's government directly responsible for a May 14 attack by Islamist guerrillas on an Indian army base, in which 30 people were killed.</P>
<P>But Vajpayee and his right-wing supporters have wider aims. They think a war would allow them to establish complete control over Kashmir--and force the U.S. out of its cozier relationship with Pakistan since September 11 and into a longer-term strategic alliance with India.</P>
<P>This is why Vajpayee--taking his cue from Israeli Prime Minister Ariel Sharon--has dismissed all concessions from Pakistan as "insufficient."</P>
<P>Musharraf has bowed to India's demands on a number of occasions. But Pakistan's military and security services do have numerous ties to Islamist groups. And Musharraf, who came to power in a coup, fears that he could be ousted if he backs down from a fight.</P>
<P>As <I>Socialist Worker</I> went to press, Musharraf and Vajpayee were headed to a summit in Kazakhstan, where Russian leader Vladimir Putin has offered to mediate talks. Meanwhile, top U.S. officials--military and diplomatic--were being dispatched to the Indian subcontinent to "urge restraint."</P>
<P>But don't get the idea that the U.S. and Russian governments are genuinely concerned about peace. Over the past year--and especially since September 11--both have added fuel to the fire.</P>
<P>Last February, the U.S. broke its 50-year-old ban on the sale of weapons systems to India, and Russia announced that it would give India "a stake in its programs for developing new generation weapons," according to a report by the Press Trust of India.</P>
<P>Meanwhile, after abandoning its Taliban allies in Afghanistan, Pakistan became a full-fledged member in the U.S. "coalition against terrorism"--even if that meant Washington ignoring the crimes of Musharraf's military dictatorship.</P>
<P>George W. Bush's war drive set the stage for the latest conflict between India and Pakistan--now with millions of lives and a nuclear nightmare at stake.</P>

<font face="Times New Roman, Times, serif" size="4"><b><P>Roots of the conflict in Kashmir</P></b></font>
<font face="Times New Roman, Times, serif" size="3"><P>INDIA AND Pakistan have fought four wars. Three of them were over Kashmir, a region that has been partitioned between India and Pakistan since the two countries were formed after British colonialists left South Asia a half-century ago.</P>
<P>India originally gained control over the predominantly Muslim state through a political double cross. Despite numerous promises of autonomy, New Delhi has ruled with an iron fist. In just the last decade, some 80,000 people, mainly Muslims, have died as a result of India's occupation.</P>
<P>Across the border, Pakistan claims to want "self-determination" for Kashmir. But even "liberal" Pakistani leaders, like former Prime Minister Benazir Bhutto, oppose independence as an option. </P>
<P>Meanwhile, the Pakistani military continues to arm Islamist organizations to challenge Indian control in Kashmir. These groups have little popular support on the ground, as they are rightly seen as lackeys of Pakistan. </P>
<P>And the secular, left wing of the liberation struggle has been seriously weakened through years of Indian repression. </P>
<P>There will be no peace in Kashmir until both India and Pakistan withdraw their troops and allow a free referendum. But this will only happen if the liberation struggle wins support from workers in both Pakistan and India, who have nothing to gain from the occupation of Kashmir and the constant threat of war.</P>

