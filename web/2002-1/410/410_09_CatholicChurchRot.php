<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The Church's rot from top to bottom</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 7, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IT'S HARD to think of a more reactionary institution than the Catholic Church.</P>
<P>The Church is the world's largest absolute monarchy. It demands conformity to a set of moral and religious beliefs invented centuries ago. To the pope and his princes, the bishops, contraception is equivalent to genocide. Yet they seem to think that sexual abuse of children is a mere public relations problem to be swept under the rug.</P>
<P>Revelations that American Catholic leaders have protected serial child molesters and paid out more than $1 billion to buy their victims' silence is sickening enough. But bishops' attempts to dodge responsibility condemn them even more.</P>
<P>One protector of pedophile priests in Dallas asserted that children as young as 6 aren't blameless in abuse cases because, under Catholic doctrine, they have reached the "age of reason" where they know the difference between right and wrong. Chicago Cardinal Francis George made a distinction between a priest who preys on kids and one who "under the influence of alcohol engages in an action with a 17- or 16-year-old young woman who returns his affection."</P>
<P>Faced with statements like this, millions no doubt wonder what planet these people think they live on.</P>
<P>The Church may have been almost 500 years too late in pardoning Galileo for the heresy of asserting that the earth revolves around the sun. But it's thoroughly up-to-date when it comes to crisis-managing the abuse scandal.</P>
<P>Its blame-the-victim courtroom tactics in abuse victim lawsuits would make William Kennedy Smith's lawyers blush. And just last week, the LA archdiocese announced a six-figure retainer to Sitrick &amp; Co., a big-name PR firm that specializes in helping clients weather bad publicity. Sitrick's client list includes gay-bashing Dr. Laura Schlesinger and the murderous Riverside County sheriff's department.</P>
<P>If the U.S. bishops are pulling up the drawbridges around their castles, it's because of--not in spite of--the feeble man at the top. Since taking over the Church in 1978, John Paul II has instituted an internal regime as intolerant as the Stalinist governments Western leaders credited him with undermining.</P>
<P>When the pope summoned the U.S. cardinals to Rome for an emergency meeting on the scandal in April, many thought he'd urge them to clean house. Instead, he ordered them to resist demands for change and provided them with "some wiggle room," as George put it, in dealing with pedophile priests.</P>
<P>The pope hasn't apologized for anything. "[T]he pope declined to let [Boston's] Cardinal [Bernard] Law resign because he feared it might give the laity the idea their opinion mattered. Cardinal Law promptly marched home and quashed efforts by restive Boston Catholics to organize an association of parish councils," wrote the <I>New York Times'</I> Bill Keller.</P>
<P>Not long after--and with the Vatican's encouragement--the Boston archdiocese rejected a $30 million settlement with abuse victims. Like a slimy tobacco company, the archdiocese would rather win a war of attrition against plaintiffs than admit it did anything wrong.</P>
<P>The scandal has caused a crisis of confidence among the 63 million U.S. Catholics. Fortunately for them, most already ignore Rome's antediluvian pronouncements on birth control, abortion, homosexuality, the role of women or premarital sex.</P>
<P>And now millions are realizing (if they didn't know it already) that the supposed holy men who claim to lead them have as much integrity as Enron's Kenneth Lay.</P>
<P>If the scandal shatters the aura of divine power that surrounds the church's leaders and priests, that's all to the better. This aura fed a culture that allowed priests to victimize children and reinforced the victims' feelings of guilt and shame.</P>
<P>The scandal has not only exposed the rot inside an ancient institution but the anti-rational underpinnings of religion itself.</P>

