<font face="Times New Roman, Times, serif" size="4">Washington's flag-waving provides a smokescreen for...</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>What their war is really about</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 7, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE OPERATION to recover remains at the site of the World Trade Center in New York ended last week with a strange ceremony--a steel girder from one of the towers was draped in a flag and buried.</P>
<P>This ending to the cleanup effort was a perfect illustration of the way that U.S. political leaders exploited the World Trade Center tragedy to whip up patriotism. But nine months after September 11, the politicians' attempts to wrap themselves in the flag are ringing increasingly hollow.</P>
<P>More and more people have begun to question the actions that Washington has taken in the name of "justice" and "security"--from a worldwide war abroad to the shredding of civil liberties at home.</P>
<P>For example, it gets harder with each passing week for the Bush White House to claim that its "war on terror" is connected to September 11. Not after the war drive expanded from Afghanistan to include Iraq, Iran, North Korea, Somalia, Cuba, Colombia, the Philippines and half a dozen other countries from one end of the globe to the other.</P>
<P>Likewise, even the gutless U.S. media has begun to report on the U.S. oil company interests at stake in the war. European newspapers reported last year that administration officials had begun to draw up plans for a war on Afghanistan before September 11--when the Taliban government rejected Big Oil's plans for a pipeline.</P>
<P>That story is only beginning to be exposed now--in the wake of revelations that the administration had substantial warnings about the September 11 hijackings but did nothing to stop them.</P>
<P>Attorney General John Ashcroft is trying to use this scandal to step up the war at home. His plan to "reorganize" the FBI is a cover for scrapping the restrictions that Congress imposed in the 1970s after the bureau's COINTELPRO program of espionage and harassment against political activists was exposed.</P>
<P>No less a right-wing ideologue than the <I>New York Times'</I> William Safire argued this week that handing the FBI and police more power is the real danger. "Some sunshine libertarians are willing to suffer this loss of personal freedom in the hope that [Ashcroft's] rules of intrusion may prevent a terror attack," Safire writes. "They won't because they're a fraud."</P>
<P>Of course, Safire supports the use of U.S. government power--as long as it's aimed abroad. But for opponents of the Bush war at home and abroad, these are two sides of the same coin--the pursuit of aggression around the world in defense of U.S. corporate interests goes hand in hand with the effort to silence opposition at home.</P>
<P>As the famous journalist H.L. Mencken once wrote, patriotism "rests firmly on the doctrine that the end justifies the means--that any blow, whether above or below the belt is fair against dissenters from its wholesale denial of plain facts."</P>
<P>The Bush gang exploited September 11 at every step to silence "dissenters from its wholesale denial of plain facts." But the real aims of Bush's war drive are being exposed.</P>
<P>And those who stood up against the war from the beginning--in the face of abuse and slander--should take confidence from the fact that we are being proved right in the eyes of growing numbers of people.</P>

