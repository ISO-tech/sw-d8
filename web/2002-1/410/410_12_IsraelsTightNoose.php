<P><font face="Times New Roman, Times, serif" size="4">Tightening the noose:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Israel orders daily raids in West Bank</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 7, 2002 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE ISRAELI government is taking new steps to tighten its iron grip.</P>
<P>With CIA Director George Tenet arriving on another "peace" mission for the Bush administration, Israeli leaders filled the airwaves with demands for Palestinian Authority chief Yasser Arafat to do more to stop "terrorism." </P>
<P>But the Israel Defense Forces are showing who the real terrorists are--with almost daily raids on Palestinian towns and seizures of Palestinian land. On May 31, Israeli tanks and troops poured into the Palestinian town of Nablus and surrounded the neighboring Ain Beit Ilma refugee camp.</P>
<P>Soldiers used loudspeakers to command all Palestinian males between the ages of 15 and 45 to walk, with their hands raised, to a field in front of the camp. Journalists said they saw as many as 2,000 men under the guns of Israeli troops--before the reporters were ordered out of the area. An estimated 400 Palestinians were handcuffed, blindfolded and taken away--for "questioning," according to Israeli officials.</P>
<P>Meanwhile, Israel's latest security plan bans travel between cities in the West Bank without a permit--which is only good from 5 a.m. to 7 p.m. At the same time, the Israeli army has quietly taken over significant tracts of Palestinian land in the West Bank, according to an Associated Press report.</P>
<P>Many of the takeovers are concentrated along the "Green Line" between Israel and the West Bank--possibly in preparation for the proposal to seal off the Occupied Territories with a new Berlin Wall. But other seizures are scattered throughout the West Bank, adding to the patchwork of Israeli settlements, "zones of control" and bypass roads that slice up the territory, making a mockery of talk about a viable Palestinian state.</P>
<P>Even the British government's Foreign Office--hardly an outpost of solidarity with the Palestinian struggle for liberation--had to admit that Israel's policies were dividing Palestinian lands into "fenced-off cantons" and creating "South African-style 'bantustans.'" </P>
<P>But you won't hear even a whisper of the truth in Washington--where the politicians of both parties compete to declare their devotion to Israel. We need a struggle to cut U.S. aid for Israel's reign of terror against Palestinians.</P>

