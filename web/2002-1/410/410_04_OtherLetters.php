<p><a name="speech"></a><font face="Times New Roman, Times, serif" size="4"><b>They say racism is free speech</b></font></P>
<font face="Times New Roman, Times, serif" size="3">
<B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>The U.S. Supreme Court has decided to take up a case that may strike down a 50-year-old law banning cross burning in Virginia. </P>
<P>The law was originally passed as a result of racist violence and intimidation by the Ku Klux Klan. As Virginia Attorney General Jerry Kilgore said, "Burning a cross to intimidate someone is nothing short of domestic terrorism." </P>
<P>Meanwhile, such racist attacks are being defended by the American Civil Liberties Union (ACLU), which calls the case "a classic free speech case." They argue that cross burning, like flag burning, is an action with political meaning and should therefore be protected. </P>
<P>Unlike flag burning, however, cross burning is historically connected to incidences of targeted violence, intimidation and fear. </P>
<P>We cannot rely on "liberal" groups like the ACLU to combat the growing arrogance and confidence of racists. Like the hundreds of thousands of French citizens who took to the streets in protest against the racist Le Pen and his supporters, we must take direct action to combat racism and fascism.</P>
<B><P>Eric Pelkey, </B>Caribou, Maine</P>

