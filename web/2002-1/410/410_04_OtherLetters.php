<p><a name="forms"></a><font face="Times New Roman, Times, serif" size="4"><b>Socialists fight all forms of oppression</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>I was both heartened and disturbed by the opinion expressed by Bruce Reilly's recent letter (<I>SW,</I> May 10).</P>
<P>He rightfully points out that one of the things that determines the quality of health care is class. This is absolutely true. But Reilly goes on to say that the "idea of a minority struggle is a trick."</P>
<P>I believe that in the kind of world that socialists are trying to create, race would not be an issue--everyone would be treated with dignity, and recognition of peoples' worth would be automatic.</P>
<P>But to think that we can say that there is no "minority struggle" in the present world is na&iuml;ve. </P>
<P>It is true that there are people of color who are part of the ruling class, but this doesn't mean that racism doesn't affect most minorities. In fact, most minorities face a double burden of racism and class discrimination on a daily basis.</P>
<P>Proof of this can be seen every day in the disproportionate percentage of young Black men in our injustice system, the military guardianship of the U.S.-Mexico border, the high poverty rate among minorities, and the racist attacks that are committed just because of the color of someone's skin.</P>
<P>Race is not the biggest factor in the oppression of working people, but as a movement, we must be sensitive to the effect that it has on people. We must strive to eliminate racism and fight for a world where workers will be treated fairly, regardless of their race.</P>
<B><P>Maritza Marquina, </B>Los Angeles</P>

