<P><font face="Times New Roman, Times, serif" size="4"><b>Sharon and Hitler aren't the same</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>Eric Ruder's article on the distinction between anti-Semitism and anti-Zionism (<I>SW,</I> May 10) was absolutely correct to point out that no one should fear being labeled an anti-Semite for opposing Israel's barbarous occupation. But I disagree with using the chant "Sharon and Hitler are the same, the only difference is the name."</P>
<P>Concern about being labeled anti-Semitic is not a good reason to oppose this slogan. A better reason is that it's not true.</P>
<P>Ruder says that we shouldn't let the differences between Sharon and Hitler overshadow their similarities. But the slogan doesn't merely point out these similarities, it equates the two. Sharon is not a fascist. He's a mainstream Zionist politician (and a war criminal).</P>
<P>On one level, the slogan seems to be an effort to avoid anti-Semitism. After all, what better way than to denounce both Nazis and Zionists in one chant?</P>
<P>Ultimately, however, the chant fails because it proposes a false comparison. Let's face it--if we really felt that Zionists and Sharon supporters were fascists, we'd be organizing pickets of most synagogues in the U.S. </P>
<P>This is not to say that <I>SW</I> has been wrong to draw parallels between the oppression and resistance of Jews in Nazi Germany and of Palestinians today--even Israeli soldiers are making the same comparisons! </P>
<P>The brutality and racism of the Israeli state should not be downplayed. But that doesn't mean we should start confusing Nazis with Zionists.</P>
<P>Socialists need to fight oppression wherever it exists, but we also need to be clear about the causes of that oppression and how best to fight them.</P>
<B><P>Dave Courtenay-Quirk,</B> Atlanta</P>

