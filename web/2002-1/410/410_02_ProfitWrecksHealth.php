<P><font face="Times New Roman, Times, serif" size="5"><b>How profit wrecks U.S. health care</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 7, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"IN THE United States, too many working-age people lack insurance coverage, and when they do get necessary medical care, it's too little and too late." That's the conclusion of "Care Without Coverage: Too Little, Too Late," a recent study published by the Institute of Medicine (IOM).</P>
<P>And the grim consequence: More than 18,000 people die each year in the U.S. because they lack medical insurance.</P>
<P>According to the study, people who are uninsured are less likely to be admitted to the hospital in the event of a serious trauma--and as a result nearly 40 percent more likely to die. People who are uninsured and diagnosed with breast or colon cancer are 50 percent more likely to die as those with insurance, and uninsured people with chronic illnesses such as diabetes, kidney disease, and heart disease are often forced to skip checkups and "ration" medications--leading to a higher incidence of serious health problems.</P>
<P>But even if you have health insurance, you can still receive substandard health care--if you have the misfortune to end up in a for-profit hospital. Researchers at McMaster University in Ontario, Canada, found that patients in for-profit hospitals in the U.S. are 2 percent more likely to die than patients in nonprofit hospitals. This may not sound like a big deal, but taking into account the millions of people treated in U.S. hospitals, it means that thousands die needlessly every year.</P>
<P>Health care should be a right for all, not a privilege for the rich--and not run by bosses who only care about the "bottom line."</P>

