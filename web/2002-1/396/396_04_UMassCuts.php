<P><font face="Times New Roman, Times, serif" size="4">Education funds are under the knife in Mass.</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>They cut back, and we pay the price</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 1, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>I am a student at the University of Massachusetts in Boston. I was shocked last week to hear that the university is planning to raise student fees more than $300. This is the third increase in two months!</P>
<P>Like me, most students at UMass Boston work full- or part-time. Many have families to take care of. It's already tough enough for working people to come up with the money for fees, tuition and books (which can be hundreds of dollars per semester!).</P>
<P>The reason for skyrocketing prices? Acting Gov. Jane Swift, in an attempt to make up for a $2 billion shortfall in the state budget, has slashed nearly $30 million from the UMass budget.</P>
<P>The university is now forced to consider eliminating entire departments and merging others. Dozens of employees have been laid off, and more layoffs are promised. They've also imposed a hiring freeze.</P>
<P>In a city like Boston, where many private colleges demand $30,000 to $40,000 annually, UMass is critical for working-class people. But at this rate, it looks like the days of affordable education may be numbered.</P>
<P>These cuts are just part of a long list of programs under the knife in the state. For example, mental health care for the poor has been cut and 10 percent of the state's social workers have been laid off.</P>
<P>You won't catch Gov. Swift tightening her belt, though. She's forced statehouse employees to baby-sit her children and even "borrowed" the state's emergency helicopter so she wouldn't have to drive home in holiday rush hour traffic! She's also used her position to avoid paying highway tolls--the same tolls she wants to double for the rest of us!</P>
<P>Jane Swift is a smug hypocrite. We need to fight her attacks on working people. The best way to shave a few bucks off the state's budget is to tell Swift to pack her bags!</P>
<B><P>Susan Fitzgerald,</B> Boston</P>

