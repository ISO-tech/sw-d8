<P><font face="Times New Roman, Times, serif" size="5"><b>Port of Seattle</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nat Gibbons</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 1, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SEATTLE--Port Commissioner President Clare Nordquist last August postponed a decision to privatize the cranes at the Port of Seattle to "give the unions and the Port time to develop ways of cutting costs without eliminating jobs." But 35 men and women still lost their jobs at the Port January 15.</P>
<P>Steve Weber and James Simmons are longtime crane mechanics who were laid off following the leasing of cranes to Stevedoring Services of America because money</P>
<P>was "tight." But the Port was able to scrounge up $100,000 to bring in Labor Relations Director Lou Paisano to "bust the union," according to one former worker.</P>
<P>In the face of management's offensive, the International Union of Operating Engineers Local 302 wasn't much help. Weber, who worked at the Port for eight years, quoted his union's business agent as saying, "I will not spend one dime of the union's money fighting for these jobs."</P>
<P>In spite of the lack of support from their local, the crane mechanics have teamed up with rank-and-file electricians and community activists to broaden the fight at the Port. They held an August 10 rally on the steps of the Port of Seattle. That momentum was lost in the wake of September 11, and Port commissioners went through with the privatization without much public response.</P>
<P>According to Weber and Simmons, the 19 jobs lost from the International Union of Operating Engineers will sharply cut into retired workers' pensions. In addition, Weber told <I>Socialist Worker</I> that there are 13 labor organizations on the docks without ratified contracts for their workers.</P>
<P>The fact that the crane mechanics, as well as so many other workers at the Port, don't have contracts leaves them in a weak position. That's why it's so important the mechanics and electricians haven't stayed quiet.</P>
<P>In the past 15 years, the Port of Seattle has turned over half a billion dollars, in taxpayer-funded upgrades, to private corporations. So the Port Profits for Human Needs Campaign--a coalition of labor and community groups--is demanding that the Port's profits go to help people in Washington State instead of business.</P>
<P>The laid-off workers plan to demand their jobs back and are counting on other unions--like the Teamsters who take goods to and from the Port--to show their solidarity.</P>
<P>The experience has galvanized workers, like Jim Weber, who has a clear message for other union members: "Stand and fight!"</P>

