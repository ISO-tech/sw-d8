<P><font face="Times New Roman, Times, serif" size="5"><b>An Indian court's contempt for Arundhati Roy</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 1, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>Author and activist Arundhati Roy is facing an important court trial in the beginning of March. The Indian Supreme Court is charging Roy for contempt in an attempt to silence her.</P>
<P>Her outspoken opposition against corporate globalization and its effects on India has made her a target of the Indian state. According to the court system, her essay "The Greater Common Good" on the Narmada Dam controversy "polluted the stream of justice."</P>
<P>It is clear that this is a political trial. Roy has long attracted the attention of officials from her tireless efforts in exposing the effects of corporations like Bechtel and Enron.</P>
<P>The court has been closed off to the public, except for Roy's husband and her lawyers. In pretrial hearings, she was heckled and harassed by judges. Activists in India fear a strong court bias against her.</P>
<P>We cannot let a court anywhere do this to a comrade in the fight against corporate globalization. Activists in India are asking people all around the world to publicize this trial. Local groups should find ways to protest the harassment of this fighter on March 6.</P>
<B><P>Douglas Murray,</B> Amherst, Mass.</P>

