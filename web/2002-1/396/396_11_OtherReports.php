<P><font face="Times New Roman, Times, serif" size="4"><b>University of Texas tuition hike</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Warren Craig</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AUSTIN, Texas--The University of Texas (UT) Board of Regents passed the largest increase in student fees in UT-Austin's history February 14. The increase will cost most students an additional $230 a semester, with incremental increases of $50 per semester over five years.</P>
<P>In the week leading up to the board's decision, student activists waged an inspiring campaign to stop the increase, winning the support of a majority of UT students. In less than six days, more than 2,700 signed a petition demanding that the board vote against fee increases, and more than 100 rallied on campus.</P>
<P>With banners that read "Keep education affordable" and "Reclaim our school," students and staff spoke out about the effects of the increase in a time of recession and about the undemocratic decision-making process. After the rally, 60 protesters marched into the tower and up to the president's office, chanting "Stop the fee!"</P>
<P>The strength of our action, considered illegal under UT's stone-age "free speech" policies, forced UT President Larry Faulkner to address the crowd of protesters in front of his office.</P>
<P>After the defeat, many students became even angrier at the UT administration and the legislature, who together have declared war on affordable, quality education.</P>

