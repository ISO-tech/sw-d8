<P><font face="Times New Roman, Times, serif" size="5"><b>No racist scapegoating of Arabs and Muslims</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sheridan Lowell</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 1, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--A young, multiracial crowd of 400 people rallied February 20 as part of a National Day of Solidarity with Muslims, Arabs and South Asian Immigrants.</P>
<P>The rally, held in front of the Immigration and Naturalization Services (INS) offices in downtown New York, was called by a coalition of antiwar, anti-police brutality, civil rights, Arab, Muslim and South Asian organizations--including DRUM (Desis Rising Up &amp; Moving), the October 22 Coalition Against Police Brutality and Help and Hope Pakistani Community Organization.</P>
<P>As part of its "war on terrorism," the U.S. has detained hundreds of immigrants on unknown charges and alleged visa violations. "We need to connect what's happening to immigrants with racial profiling," Monami Maulik of DRUM told <I>Socialist Worker</I>.</P>
<P>"The issue of detention could tie together the consciousness of immigrants and nonimmigrants. And ties need to be built to the anti-prison movement because of the criminalization of people of color and immigrants. It's the same system of exploitation."</P>
<I><P>Check www.drumnation.org on the Web for information about a March rally at INS offices in Passaic, N.J.</P></I>

