<P><font face="Times New Roman, Times, serif" size="4">White House sued over energy task force</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>What is Cheney hiding?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 1, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CONGRESS IS suing the White House for the first time in history to find out who wrote the Bush administration's energy policy. Since last summer, Vice President Dick Cheney has refused to respond to routine requests from the General Accounting Office (GAO)--the nonpartisan investigating agency of Congress--for information about a White House task force on energy issues that he chaired.</P>
<P>The task force proposal, put forward last year, included support for oil drilling in the Arctic National Wildlife Refuge, phony "clean coal" technology, further deregulation of the energy industry and even a revival of nuclear power.</P>
<P>It's not hard to guess who came up with that--Big Oil and the other honchos of the energy industry. But Cheney won't say who served on the task force. His lame defense is that members would never talk freely if they knew that their role was going to be made public.</P>
<P>"The way Dick Cheney tells it, energy tycoons are timid souls, reluctant to give opinions and terrified someone might find out they have been at the White House," <I>Washington Post</I> columnist Mary McGrory summarized. "Only when the shades are drawn and the tape recorder is off will they confide that they are against regulation of the energy market. It takes a threat of the rack to make them even mention a tax break for their companies or suggest names for big jobs in government."</P>
<P>What do we know already? We know that Cheney avoided meeting with representatives of environmentalist groups such as the Sierra Club and the Union of Concerned Scientists until after he had released his proposals. We know that the Enron gang had a big voice in White House energy policy--meeting with Cheney and his staff six times last year.</P>
<P>Other good bets for task force members are the fat cats at Unocal and Halliburton--the oil services company where Cheney himself raked in millions as CEO before moving to the White House. Unocal and Halliburton are the two main companies behind the plan to build a natural gas pipeline from the Caspian Sea region across Afghanistan and Pakistan to the Indian Ocean.</P>
<P>The Bush administration reportedly reopened negotiations over the pipeline deal with the Taliban in Afghanistan last year. Maybe Cheney and Co. don't want records showing that the White House was courting the regime that became "enemies of freedom" after September 11.</P>

