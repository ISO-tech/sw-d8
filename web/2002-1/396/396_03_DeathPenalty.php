<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Barbarism of the death penalty</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 1, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WHO'S MORE insane? Andrea Yates or Chuck Rosenthal?</P>
<P>Yates is the Houston woman who killed her five children last year, drowning them one by one in the bathtub, then calling police and confessing. Rosenthal is the Harris County, Texas, prosecutor who asked for "wisdom from God"--then decided that it was his solemn duty to seek Yates' execution.</P>
<P>Yates' trial is underway now, but even in Texas--the death penalty capital of the U.S.--polls show that most people don't want a mentally ill woman put to death.</P>
<P>Yates had a history of postpartum psychosis and heard voices telling her to "get a knife." Two weeks before the murders, a doctor took her off anti-psychotic medication and put her on weaker antidepressants. Yates' husband begged the doctor to prescribe stronger medications, but to no avail.</P>
<P>"What are we saying by prosecuting this woman?" asked columnist Molly Ivins. "That we don't think there is such a thing as mental illness?&#133;Do people think she would be 'getting away' with murder? Do they think she's faking her illness? What possible solution to this tragedy can be offered by the criminal 'justice' system?"</P>
<P>As Yates sat in court last week, the U.S. Supreme Court took up the issue of whether the mentally retarded should be executed, hearing arguments in an appeal from a Virginia death row prisoner. The debate focused on whether a national "consensus" had formed against executing the mentally retarded because a majority of states now ban such executions.</P>
<P>But one justice, David Souter, asked a question that went to the heart of the issue--if retarded people with the mental age of 5 can be executed, does that mean that 5-year-olds should be executed, too?</P>
<P>Sure, said Virginia Assistant Attorney General Pamela Rumpz, arguing against the appeal, if they "know the difference" between right and wrong. Examples like these show the barbarism of the death penalty.</P>
<P>What purpose does the death penalty serve? It doesn't deter crime. It's used almost exclusively against the poor and disproportionately against minorities. It threatens to take the lives of the innocent.</P>
<P>But the death penalty is good for promoting the careers of ambitious politicians like Chuck Rosenthal and Pamela Rumpz.</P>
<P>Or the Arkansas governor who in 1992 approved the execution of a mentally retarded man named Ricky Ray Rector. Before going to the execution chamber, Rector saved the dessert from his last meal--to have when he came back to his cell.</P>
<P>The Arkansas governor was Bill Clinton--and he used Rector's execution to prove his law-and-order credentials during his campaign for the presidency.</P>
<P>But support for the death penalty, overwhelming 10 years ago, has eroded. No reforms can ever make this system just. The death penalty should be abolished.</P>

