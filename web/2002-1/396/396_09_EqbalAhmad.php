<P><font face="Times New Roman, Times, serif" size="5"><b>World terrorism created by the U.S.</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Keeanga-Yamahtta Taylor</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 1, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>BOOKS:</B> Eqbal Ahmad, <I><B>Terrorism: Theirs and Ours,</B></I> forward and interview by David Barsamian, Seven Stories Press, 2001, 59 pages, $6.95.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>SEVEN STORIES Press has given antiwar activists an invaluable resource by republishing interviews by the late anti-imperialist fighter Eqbal Ahmad.</P>
<P>While this short book is composed of material written well before September 11, Ahmad provides sharp insight as to what may have motivated the attacks and, furthermore, as to why the U.S. can't be trusted to wage wars for "democracy."</P>
<P>Even before opening the book, we're reminded that, as much as George W. Bush characterizes the Taliban as "evildoers," the U.S. once viewed them as allies. The book's cover is a photo of President Ronald Reagan and the Afghan mujahideen--later to become the Taliban--chatting in the Oval Office.</P>
<P>A 1998 speech by Ahmad reprinted here describes how the U.S. created the enemy they now claim to be fighting against.</P>
<P>"[T]he notion of jihad as 'just struggle' had not existed in the Muslim world since the 10th century until the U.S. revived it during its jihad against the Soviet Union in Afghanistan&#133;The U.S. saw it as an opportunity to mobilize the Muslim world against communism&#133;Since then, almost every Islamic militant, including those in Israel, Algeria, and Egypt&#133;has been trained in Afghanistan. The CIA calls it 'Islamic blowback.'"</P>
<P>Ahmad describes how, in the 1980s, the CIA considered Osama bin Laden a "prize recruit" when he was fighting the Soviets.</P>
<P>Statements Ahmad made years ago seem to speak directly to Bush's war for "justice" today. "The U.S. concern is not who is fundamentalist and who is progressive, who treats women nicely and who treats them badly&#133;The issue is who is more likely to ensure the safety of the oil resources that the U.S. or its corporations could control?"</P>

