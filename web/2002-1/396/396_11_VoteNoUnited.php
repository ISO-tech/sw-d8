<P><font face="Times New Roman, Times, serif" size="4">New proposal opens the way to concessions</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Vote down United's deal!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jennifer Biddle, IAM Local Lodge 1781</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 1, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MECHANICS AT United Airlines--who were euphoric two weeks ago because they thought they beat back one of the biggest, meanest corporations in the U.S. and George W. Bush--are now reeling.</P>
<P>It appears that our union leaders are complicit yet again in peddling a concessionary contract. There is history here.</P>
<P>In 1993, the International Association of Machinists (IAM) orchestrated a "work safe" campaign during contract negotiations. I was a student intern at United and got to witness what nearly amounted to a sit-down strike.</P>
<P>Despite mobilizing people for a work slowdown, the IAM recommended the Employee Stock Ownership Plan (ESOP) to avoid United's threats of massive layoffs. In exchange for stock in the company and a seat on the Board of Directors, the union agreed to concessions amounting to $4.5 billion.</P>
<P>Fast forward to 2002. We have endured eight years of concessions, a booming economy, more than two years of negotiations, a temporary restraining order that has prevented mechanics from "working safe," and the promise all along by United that we would have a seamless contract with industry-leading wages.</P>
<P>After all of this, our union is recommending we ratify a tentative agreement (TA) that allows the company to come up with a "financial recovery plan" that includes more concessions, leaves quality of life issues vague, and makes it easier to outsource work overseas.</P>
<P>Since the company laid off 20,000 employees last October, many mechanics have been forced into working six days in a row, with rotating days off and no overtime pay--a contract violation. The union has filed a grievance, but the procedure can take years.</P>
<P>After the last strike vote, the union took "exit polls" to determine which issues to fight for. The infamous 6/2 schedule was a major concern. But the TA simply states: "It is in the best interest to fully explore these issues."</P>
<P>The TA also contains language that would allow rotating planes to overseas routes where our heavy maintenance could be performed.</P>
<P>The main issue mechanics are fighting over--and the worst part of the contract--concerns wages. The TA includes a 37 percent raise. But it states that United only has to declare itself in a "severe financial condition" for the union to be forced into negotiating concessions that give the raises back.</P>
<P>United has yet to prove that it needs to file for bankruptcy. The company's chief financial officer refused to fully disclose United's financial state to the Presidential Emergency Board. Moreover, the fact that Enron's accounting firm, Arthur Andersen, does United's books hasn't been lost on those employees sporting "Fly Enron Airline" buttons.</P>
<P>The stronger a contract we have now, the better off we will be even if United does file for bankruptcy.</P>
<P>One of the main reasons mechanics rejected George W. Bush's Presidential Emergency Board recommendation by 68 percent and voted 86 percent to strike is because the deal included similar language on future concessions.</P>
<P>The main difference in the TA seems to be that mechanics will get to ratify their concessions. But what happens if we vote for the TA now and want to refuse concessions later? If we give up our right to strike over this contract with United Airlines, we will give up everything.</P>
<P>The unfortunate reality is that when it comes down to supporting us over the bread-and-butter issues that we are willing to strike over, the International seems just as willing to cheat us as the company.</P>
<P>We were set to walk out February 20--the only thing that could have stopped us was Congress. And with President Bush touring Asia, Congress out of session for 10 days, and the 2002 Winter Olympics ending, we had the perfect opportunity to show the "Official Airline of the U.S. Olympic Team" just how powerful we are.</P>
<P>Instead, the union announced the tentative agreement some 36 hours before the strike deadline--and they came back with an agreement that makes it easier for the company to get concessions.</P>
<P>Since July 12, 2000, when our contract became amendable, our right to strike has been our only leverage against United Airlines. Ultimately, this is why we must vote no to the tentative agreement and yes to a strike on March 7.</P>

