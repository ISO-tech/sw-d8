<P><font face="Times New Roman, Times, serif" size="4">States slash spending</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>These cuts are going to wreck lives</P></b></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 1, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>STUDENTS AND teachers in the Los Angeles Unified School District got an ugly surprise in January when the school board suddenly announced a $56 million cut in its budget.</P>
<P>"This means an immediate end to our after-school programs for children who are behind in reading and math," said Michal Myers, a teacher at a South Central LA elementary school and chapter chair in United Teachers Los Angeles.</P>
<P>Plus, if schools superintendent Roy Romer gets his way, another $92 million cut will hit soon. "And the cuts also mean that we're being offered no raise by the school district--not even a cost-of-living increase," Myers said.</P>
<P>The cuts in LA are just one example of the budget crisis in California. The crisis was made much worse by the payment of billions from the state surplus to corporate speculators like Enron during last year's energy crisis--money that had been earmarked for education.</P>
<P>But other states are suffering, too. Across the U.S., states face a combined shortfall of up to $50 billion--in large part because of tax breaks handed out to corporations and the wealthy during the 1990s, according to economist and <I>New York Times</I> columnist Paul Krugman. "States cut taxes that bear most heavily on upper-income families," he wrote. "The end result was a redistribution of the tax burden away from the haves toward the have-nots."</P>
<P>And those have-nots will soon have even less, if governors and state legislatures get their way. As the <I>Los Angeles Times</I> reported this week, several state governments have already frozen enrollment in the federal children's health insurance programs, which were created five years ago to cover kids whose families don't qualify for Medicaid.</P>
<P>And the Center on Budget and Policy Priorities reported in January that 19 states have imposed cuts in services and programs for low-income people. In Illinois, Gov. George Ryan plans to slash $500 million for health services for the poor--including the closure of centers for the developmentally disabled and mentally ill.</P>
<P>State governors this week appealed to the federal government for emergency aid to shore up Medicaid, which covers one-third of births and two-thirds of nursing home patients. But the Bush administration wants more cuts, not fewer.</P>
<P>That's because the White House has different priorities. Military spending in 2002 alone will increase by $45 billion--to nearly $400 billion.</P>
<P>It's time to take a stand against budget cuts that hammer the poor--while the war makers cash in. We need to fight for different priorities--ones that put the needs of working people first.</P>

