<font face="Times New Roman, Times, serif" size="5"><b>Israel's terror</b></font></P><p><font face="Arial, Helvetica, sans-serif" size="2">March 1, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WHEN MAYSOUN Hayek went into labor at 1:30 a.m., her husband and father-in-law decided to rush her to the hospital.</P>
<P>But Maysoun and her family are Palestinians living in the West Bank. So racing to a nearby hospital meant crossing Israeli checkpoints--a dangerous and potentially deadly challenge for Palestinians at any time, but especially at night.</P>
<P>The three made it through the first checkpoint without incident. Then, suddenly, their car was caught in a hail of gunfire. Maysoun was hit in the shoulder, her father-in-law critically wounded.</P>
<P>And Maysoun's husband Mohammed was dead--his body riddled with 25 bullets, according to X-rays.</P>
<P>Mohammed was a casualty of the Israeli government's war on Palestinians--carried out with the full backing of the U.S. government. But not the only one that week--and not even the only one at that checkpoint. Israeli soldiers shot and injured another Palestinian woman in labor one day earlier <I>at the same checkpoint</I>.</P>
<P>These shootings capped the most violent week since the new Palestinian Intifada began 17 months ago--with more than 50 people dead, most of them Palestinian. Yet though the scale of the bloodshed was high, the savagery is routine.</P>
<P>"Yesterday at this spot, the Israelis shot eight young men, six of whom were under the age of 18. One was 12," <I>New York Times</I> reporter Chris Hedges wrote in the June 17, 2001, entry in his diary of a trip to Gaza, published in <I>Harper's</I> magazine last month. "This afternoon, they kill an 11-year-old boy, Ali Murad, and seriously wound four more, three of whom are under 18. Children have been shot in other conflicts I have covered&#133;but I have never before watched soldiers entice children like mice into a trap and murder them for sport."</P>
<P>A story in Israel's <I>Ha'aretz</I> newspaper underlined the cold-blooded horror of the repression. The article described an Israeli officer telling peers to "study how the German army operated in the Warsaw Ghetto"--a reference to the Nazi's repression of Polish Jews before they were shipped off to the death camps during the Second World War.</P>
<P>And through it all, what has the U.S. government done? Pointed the finger at Yasser Arafat. Echoing the Bush administration during a trip to Israel last week, Sen. Hillary Rodham Clinton (D-N.Y.) declared that the Palestinian Authority chief had "failed as a leader."</P>
<P>That same week, Israeli Prime Minister Ariel Sharon announced his "new outline on the war on terror." The plan? More counterterrorism methods--"a euphemism to describe the work of its death squads, which have assassinated more than 70 Palestinian suspects during the conflict despite widespread criticism," wrote Phil Reeves in Britain's <I>Independent</I> newspaper.</P>
<P>Of course, neither Clinton nor any other U.S. official had any complaints about Sharon. With full U.S. backing, Israeli politicians claim again and again that they want peace--and that the Palestinians are the "terrorists."</P>
<P>But the 270 Israeli reservists who have refused to serve in the Occupied Territories have dealt a blow to this fairy tale. Their stories of atrocities regularly committed by Israeli soldiers show just who the real terrorists are.</P>
<P>"You get used to it in a hurry, and many even learn to like it," wrote reservist Asaf Oron, describing the daily routine that eventually sickened him and drove him to sign onto a petition with other so-called "refuseniks." "Where else can you go out on patrol--that is, walk the streets like a king, harass and humiliate to your heart's content&#133;and at the same time feel like a big hero defending your country?"</P>
<P>The U.S. government makes this brutality possible--with both diplomatic support and more than $3 billion in aid given to Israel annually, most of it military. We have to demand that the U.S. end its support for Israel's terror.</P>

