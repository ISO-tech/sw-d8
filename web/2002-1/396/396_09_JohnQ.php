<P><font face="Times New Roman, Times, serif" size="5"><b><i>John Q</I>: A movie for our side</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Elizabeth Lalasz</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 1, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>MOVIES: <I>John Q,</B></I> directed by Nick Cassavetes, written by James Kearns, starring Denzel Washington, Anne Heche and Robert Duvall.</P>
</font><I><P>JOHN Q</I> is an inspirational movie that socks it to the naked greed of the health care industry.</P>
<font face="Times New Roman, Times, serif" size="3"><P>Factory worker John Q. Archibald (Denzel Washington) and his family already have financial problems when John's hours are cut in half as a result of the recession. But when they find out their son needs a new heart, things go from bad to worse.</P>
<P>Most critics have panned <I>John Q,</I> arguing that the story is manipulative and not "real" enough and that director Nick Cassavetes is propagandizing for nationalized health care.</P>
<P>But <I>John Q</I> isn't meant to be subtle. It's meant to expose the horrors of managed care, and it does this in a big Hollywood film kind of way.</P>
<P>In a gripping scene, John and his wife find out that their son will die without a heart transplant. They sit on one side of the table, experiencing the worst moment of their lives. On the other side are their son's doctor (James Wood) and the hospital's administrator (Anne Heche), who are completely devoid of emotion.</P>
<P>The administrator insists on $75,000 up front just to get their son on the donors' list, since a heart transplant is "elective surgery." John and his wife try everything, from appealing to their insurance company to filing for Medicaid. Finally, they start selling their belongings, but what they collect amounts to only a fraction of what they need.</P>
<P>When the hospital wants to release his son because they haven't paid enough, John takes over the emergency room.</P>
<P>The point of <I>John Q</I> is crystal clear: There are two types of health care in this country--one for the rich and another for the rest of us. You are either with them--the profit-driven HMOs, administrators and employers--or you are with us--working people. And since they won't help us, we need to take matters into our own hands.</P>
<P>It's this overt political message that's making so many liberal critics squeamish and what makes <I>John Q</I> totally refreshing. Another strength of <I>John Q</I> is just that so many people can identify with it. How many have been screwed by an HMO? How many people would be in a similar financial crisis if a serious injury occurred in their family?</P>
<P>John Q. could be any one of us. In this way, the movie becomes an anthem for everyone who's ever been frustrated by their health coverage and gives expression to the possibility of fighting back.</P>
<P>And, despite critics going after <I>John Q</I> for simplifying its characters, it actually cuts against the stereotype of working-class people as reactionary idiots. During the ER scene, John and his "hostages" take on a character who is a racist who beats his girlfriend.</P>
<I><P>John Q</I> should be seen and praised. This is a movie for our side.</P>

