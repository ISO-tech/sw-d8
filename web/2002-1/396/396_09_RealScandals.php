<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Letting the real scandals go by</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 1, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WHEN THE Enron scandal broke in December, political pros were quick to dismiss it as a short-lived "inside-the-Beltway" tussle that wouldn't interest most Americans.</P>
<P>Turns out the pundits were wrong again. An early February Gallup Poll showed that a majority of Americans think the Enron mess is very important.</P>
<P>Ordinary people know perfectly well what Enron represents--greedy corporate criminals fleecing workers while they pay protection money to politicians. Enron represents all that's rotten about the free-market snake oil that's been shoved down our throats for decades.</P>
<P>You'd think that maybe a few Democrats would really want to hoist the Bush gang on the Enron petard. Wouldn't the so-called "party of working families" want to roast Enron for ripping off California consumers in last year's phony power crisis?</P>
<P>Instead, the Enron hearings have been tepid affairs, with Democrats tiptoeing softly around the "wartime" president and focusing on such issues as accounting rules.</P>
<P>But use the Enron scandal to roll back energy deregulation, to stop corporate tax evasion or to guarantee every worker a right to a pension? It wouldn't even cross their minds. After all, Democrats slurp from the same corporate trough as the Republicans.</P>
<P>There's a more fundamental explanation for the Democrats' caution, too. Scandal is the lifeblood of much of mainstream politics. With the two major parties agreeing on so many fundamental points, scandal mongering often replaces political debate on issues that actually matter to people.</P>
<P>But this rule of scandal mongering only applies to small-time peccadilloes like White House sex. When scandals like Enron break, they have the potential to lay bare the way the system really works--and to catch members of the ruling class in their undertow.</P>
<P>And that's when the main political guardians of that ruling class--the Democrats and Republicans--join together to shore up the system.</P>
<P>Of course investigations take place. Politicians propose reforms. Some reforms may even become law. Some crooks--usually lower-level patsies--may even spend some time in jail. All of this is a small price to pay to prevent a more penetrating look at the root causes of the scandal.</P>
<P>When the Reagan administration flagrantly flouted a congressional ban on aid to the right-wing Nicaraguan Contras in the mid-1980s, the president committed far more serious crimes than perjury about sex.</P>
<P>Yet when the Democratic-controlled Congress convened hearings on the scandal, Senator Sam Nunn (D-Ga.) announced, "We must, all of us, help the president restore his credibility in foreign affairs." After all, the Democrats wanted to show that they were as committed to fighting "communism" in Nicaragua.</P>
<P>So the hearings proceeded to reveal details of the Contragate conspiracy, but never got close to nailing its key players (like Reagan and Vice President George Bush). The hearings never challenged the Cold War rationale for all the lying and deception.</P>
<P>Even in the most serious and far-reaching modern scandal--the Watergate scandal that brought down Richard Nixon--the Democratic "opposition" helped to prop up the system. When the Democratic-run House Judiciary Committee voted articles of impeachment against Nixon, it rejected the most serious charges the scandal raised--like Nixon's secret expansion of the Vietnam War into Cambodia or major American corporations' contributions to Nixon-controlled slush funds.</P>
<P>The Enron hearings may yet turn up some surprises. And a few Bush administration figures may be knocked from their perches.</P>
<P>But if you hear politicians and major newspapers proclaim that the Enron investigations showed "the system works," be assured that a lot of powerful people are laughing all the way to bank.</P>

