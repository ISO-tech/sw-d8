<P><font face="Times New Roman, Times, serif" size="4">Protests in Puerto Rico:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Free Pedro Col&oacute;n</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By H&eacute;ctor Reyes</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 1, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>HUNDREDS OF students paralyzed two campuses of the University of Puerto Rico (UPR) February 21 in a walkout to show solidarity with student Pedro Col&oacute;n Almenas.</P>
<P>Col&oacute;n Almenas was scheduled to be sentenced the next day in federal court--for the "crime" of protesting militarism on campus and supporting the people of Vieques in their fight against the U.S. Navy's target practice.</P>
<P>He was convicted for supposedly attacking a Reserve Officer Training Corps (ROTC) major at UPR's San Juan campus during a demonstration last spring. Yet even administrators admit that Pedro is innocent.</P>
<P>His role in the incident was as a mediator between protesters and ROTC members during a fistfight--which was his official duty as student safety coordinator for the campus.</P>
<P>Pedro faced up to five years in prison for the conviction. He was given a one-year jail sentence and three years of probation, against the wishes of right-wing students who demanded the maximum.</P>
<P>But Pedro's supporters aren't satisfied. On the day of the sentencing, about 100 students and other supporters protested in front of the Federal Building.</P>
<P>One day earlier, the UPR faculty passed a resolution supporting Pedro and insisting that he not spend "even a single day in jail." Some faculty members and university workers joined students at the university gates during the strike.</P>
<P>"The sentence is political, and therefore the strike is political," said student Hugo Delgado. "Everyone sympathizes with him and knows that the real aggressors here are those from ROTC."</P>

