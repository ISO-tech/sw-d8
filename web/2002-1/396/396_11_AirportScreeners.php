<P><font face="Times New Roman, Times, serif" size="5"><b>Airport screeners defend their jobs</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Terzakis, Kevin Neel and Eduardo Capulong</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 1, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>FILIPINO COMMUNITY organizations, labor unions, antiwar coalitions and immigrants rights groups held simultaneous press conferences and rallies February 19 at airports in San Francisco, Oakland and San Jose to protest the planned firing of immigrant baggage screeners. Hundreds of people turned out in spite of rain to show their solidarity with the airport workers.</P>
<P>More than 1,000 immigrant workers in the Bay Area alone are slated to lose their jobs under the Aviation and Transportation Security Act, part of a slew of racist federal legislation passed in the wake of September 11.</P>
<P>"The law is a reactionary and irrational response to the events of 9/11," said Lillian Galedo, Executive Director of Filipinos for Affirmative Action. "We have been down this path before, with the internment of Japanese and Japanese Americans during the Second World War. We will not let it happen again."</P>
<P>Other speakers included representatives of Service Employees International Union (SEIU) Local 790, which represents screeners at San Francisco airport; local labor councils; other unions; and many other organizations.</P>
<P>The racist treatment is not new. Marina Neri, a baggage screener for four years, was recently fired for her involvement in SEIU Local 1877's organizing drive and for circulating a petition protesting management abuse at Oakland's airport. "Even before the new law," Neri told the crowd, "the management here was practicing favoritism and discrimination against immigrant workers."</P>
<P>The law is particularly cruel given the years that many airport screeners have endured earning the minimum wage. Just after unionization improved wages and benefits in San Francisco and Los Angeles, and just as the government takeover of airport screening is raising wages nationally, these hardworking people are losing their jobs.</P>
<P>Many workers didn't attend the rally, fearing victimization threatened by their managers. But a number who did were encouraged to address the crowd when they saw the support they had.</P>
<P>The next steps are still being debated. SEIU has filed a lawsuit requesting a court injunction against the layoffs and is lobbying Congress to repeal the citizenship requirement or to fast-track citizenship for screeners--a plea that has largely fallen on deaf ears in a Congress busy beating the drum of nationalism.</P>
<P>We need to focus on grassroots organizing--involving screeners themselves in building public awareness and reaching out to other union members. San Francisco--where the recent struggle to win union recognition has strengthened rank-and-file organization--will be a key battleground.</P>
<P>"We need political mobilization, community forums and flyers in restaurants and businesses," said Leticia Santo, a screener and activist at Argenbright Security during the 1999 union organizing drive. "We need to build leadership. We need to fight immigrant bashing."</P>

