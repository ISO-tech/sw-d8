<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Democrats' loyal non-opposition</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 29, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>BET YOU didn't realize that the Democrats unveiled their call to action in the battle over the federal government's budget priorities last week. That's because the alternative budget resolution proposed by Senate Budget Committee Chair Kent Conrad (D-N.D.) was so meek in its disagreements with the White House that even the partisan-obsessed Washington press corps mostly ignored it.</P>
<P>You'd think that the "friends of working people" would make increased unemployment benefits or better funding for schools the centerpiece of their proposal. Instead, Conrad and Senate Democrats focused on&#133;paying down the national debt.</P>
<P>The Democrats couldn't even agree to demand a repeal of George W. Bush's $1.3 trillion tax-cut giveaway to the rich--even though all of Washington admits that the tax breaks will push the federal budget into a huge deficit, forcing even more disastrous spending cuts in programs for workers and the poor.</P>
<P>But the pathetic failure of Democrats to put up a fight against any aspect of the Republican agenda is par for the course. From tax cuts to the Enron scandal, wrote <I>Los Angeles Times</I> columnist Ron Brownstein, "[i]t's difficult to think of a single issue where Senate Democrats have put Bush on the defensive."</P>
<P>Instead, they're so paralyzed by the fear of being challenged by Bush--who still has high popularity ratings because of the war--that they've ducked every opportunity to organize a challenge to the White House.</P>
<P>But then again, it's not true that the Democrats oppose the Republican agenda. They just want a slightly kinder and gentler version of it.</P>
<P>The Democrats supported a giant tax cut for the rich last year--just not quite as enormous as the White House proposal. They talk about the irresponsibility of Enron's crooked bosses, but they're just as beholden as Republicans to Corporate America--and just as responsible for the laws and policies that paved the way for disasters like Enron.</P>
<P>They support the "war against terrorism," they support the crackdown on civil liberties, they support increased Pentagon spending, and they support corporate welfare at the expense of workers--because the Democrats are committed to the Washington status quo of a government that serves the bosses first.</P>
<P>Yet organized labor is getting ready to pump more money down this sinkhole. Last month, AFL-CIO President John Sweeney proposed a near doubling of the money from union members' dues that goes to support for political candidates. But the Democrats have betrayed workers--again and again and again.</P>
<P>The Democrats won't promote policies in our interests--until we build a movement that forces politicians of whatever political stripe to do what we want. That's why our efforts need to be devoted to organizing a grassroots movement to oppose Washington's pro-corporate agenda.</P>

