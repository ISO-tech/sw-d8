<P><font face="Times New Roman, Times, serif" size="4">Nuclear hit list...Civil liberties gutted...Tax cuts for the rich<BR></font>
<font face="Times New Roman, Times, serif" size="5"><b>He thinks he can get away with anything</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 29, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>EVEN ESTABLISHMENT figures think George W. Bush is going too far. "Whether it is an ill-specified axis of evil, or a decision to make tactical nuclear war thinkable, or a domestic 'shadow government,' or deliberately leaked plans to attack Iraq, George W. Bush in his own way is as frightening as al-Qaeda," wrote columnist Robert Kuttner in the <I>Boston Globe.</P></I>
<P>The truth is that "fighting terrorism" is simply Washington's cover story for the U.S. to use its military power to terrorize the entire planet.</P>
<P>"We are saying that nuclear weapons are no longer the weapon of last resort but weapons of first choice," said Joseph Cirincione of the establishment Carnegie Endowment for International Peace. "It means that the nuclear nuts have seized control of the policy apparatus." "America as Nuclear Rogue," ran the headline of a <I>New York Times</I> editorial.</P>

