<font face="Times New Roman, Times, serif" size="3"><P>Bush's might-makes-right logic can also be seen in the administration's plan to imprison accused terrorists--even after they're acquitted by military tribunals.</P>
<P>"They create a tribunal that they say is fair, but then they can say, 'We don't like the results and the hell with it, we're going to hold you anyway,'" said Don Rehkopf, co-chair of the military law committee of the National Association of Criminal Defense Lawyers. "If I came out of the woods after 20 years and saw these rules, I'd think Adolf Hitler or Joseph Stalin wrote them."</P>
<P>All this comes after Bush and Congress rammed through the USA PATRIOT Act, which legalizes police-state tactics. "The danger right now is not terrorism," declared a recent editorial in the <I>Daytona Beach News-Herald.</I> "The danger is here at home, where zealotry is substituting for policy-making, where the flag is turning into the administration's fig leaf, and where slander is any opposition's reward."</P>
<P>Not that there's any opposition in Congress. Democrats have backed Bush as he pumped up the military budget to $400 billion--while spending on schools and health care are being slashed to fund tax cuts for corporations and the rich.</P>
<P>"No politician hoping for reelection will dare say it, but the administration's new motto seems to be 'Leave no defense contractor behind,'" wrote arms-control expert William Hartung.</P>
<P>But the voices of those opposed to Bush's program are beginning to be heard. At meetings in cities and campuses across the U.S., activists are organizing against both Bush's war drive and his attacks on working people in the U.S.</P>
<P>And on April 19-22, they'll travel to Washington to send a message to Bush: We're building a movement against the U.S. war machine--and renewing the struggle for global justice.</P>

