<P><font face="Times New Roman, Times, serif" size="5"><b>Stop the Nazis! Stop the Klan!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 29, 2002 | Pages 2 and 11</font></p>

<P><font face="Times New Roman, Times, serif" size="2"><B>CHICAGO</b></font><BR>
<font face="Times New Roman, Times, serif" size="3">Free Daniel Herrera Mata!</font><BR>
<font face="Times New Roman, Times, serif" size="4"><b>He was arrested for opposing the Nazis</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Bridget Broderick</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MORE THAN 200 protesters rallied outside a Chicago Public Library branch March 23 to drown out 13 Nazi followers of the white supremacist World Church of the Creator.</P>
<P>But after the racists were sent packing, Chicago cops took revenge on demonstrators--arresting two people, including a young Latino man who was charged with assaulting an officer.</P>
<P>Daniel Herrera Mata is guilty of nothing more than leading chants at the counterdemonstration--and of having the wrong skin color when cops came looking for a victim. As <I>Socialist Worker</I> went to press, antiracists were organizing to defend Mata, who remained in jail, with bail set at an incredible $100,000.</P>
<P>At the protest, a lively multiracial crowd gathered in the mostly Latino and Polish neighborhood to prevent World Church f&uuml;hrer Matthew Hale from recruiting to his goon squad.</P>
<P>In 1998, one of Hale's disciples went on a three-day shooting rampage, killing two people, including former Northwestern University basketball coach Ricky Birdsong.</P>
<P>Anti-Racist Action, the International Socialist Organization and Neighbors for Peace organized contingents for the counterdemonstration.</P>
<P>Hale's followers sneaked up to the library an hour and a half late--behind a line of Chicago cops. But protesters shouted them down, with chants like "Immigrants in, Nazis out!" and "Gay, straight, Black, white, one struggle, one fight!" Eventually, the Nazis were driven away, again protected by the cops--who were quick to turn on demonstrators.</P>
<P>Protesters said that they thought it was very important to stand up against Nazis like Hale. As Grace Corral, a student at the neighborhood high school, said, "We're Hispanic, and we need to have more rights in the U.S.--more jobs and better pay. They should let us speak out more."</P>


