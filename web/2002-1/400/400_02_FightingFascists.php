<P><font face="Times New Roman, Times, serif" size="2"><B>OWENSBORO, KY.</b></font><BR>
<font face="Times New Roman, Times, serif" size="4"><b>Stop the Klan!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Kerl</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>OWENSBORO, Ky.--The SS Knights of the Ku Klux Klan held a recruitment rally here at the Daviess County Courthouse March 23. They hoped to turn the outpouring of nationalism and racism that followed September 11 into new recruits to white supremacist violence.</P>
<P>Instead, more than 300 antiracists turned out to show their disgust for the Klan's bigotry.</P>
<P>More than 30 police from nearby cities were brought in, as well as Kentucky State Police, to help protect the Nazis as they spewed their hate. Police erected plastic fences to keep antiracist activists away from the Klan and their supporters.</P>
<P>But activists entered the supporters' area and confronted the Nazis. Humiliated, frustrated and overwhelmed by activists, the Klan quickly fled the scene.</P>
<P>Prior to the rally, a weeklong campaign of "Community Unity" was organized by city leaders, urging people to ignore the Klan and their rally. Yellow ribbons were handed out to show opposition to the Klan's racism.</P>
<P>But we need to confront the Nazis wherever they are and make sure they can't grow. "Yellow ribbons just aren't enough," LaTonya Sullivan told <I>Socialist Worker.</I> "Ignoring racists won't make them go away."</P>

