<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>We can expose the real terrorists</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 29, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE NATIONAL Mobilization for Colombia's weekend of events on April 19-22 will make a major statement against Bush's war--and for our right to dissent.</P>
<P>The biggest and broadest demonstration is needed to protest the escalating war in Colombia. But as activists mobilize, a debate about how best to build a movement against U.S. intervention has arisen.</P>
<P>The debate centers on the National Mobilization's insistence that organizations wanting to join the protest must endorse a six-point mission statement (see www.colombiamobilization.org/endorse_add.php).</P>
<P>Virtually anyone opposed to U.S. meddling in Colombia can agree with the main points--ending U.S. intervention in Colombia, ending the "war on drugs," funding drug treatment programs, supporting "sustainable economic development," and supporting refugees from the conflict.</P>
<P>But the sixth point reads: "We are committed to nonviolence in our own actions as well as supporting exclusively nonviolent, negotiated political solutions to the conflict in Colombia. We do not support or endorse any armed actor in the Colombian conflict."</P>
<P>Coupled with a preamble that states, "While we welcome and depend on the active participation of individuals and the solidarity of international activists, 'membership' is limited to U.S.-based organizations," the mission statement applies a political litmus test.</P>
<P>Opponents of U.S. intervention can--and should--have all sorts of views on the best strategies for change in Colombia. For example, this newspaper has been highly critical of the politics and strategy of the left-wing guerrilla organizations in Colombia, such as the Revolutionary Armed Forces of Colombia.</P>
<P>But our main task is to stop U.S. intervention. That should be the only rallying point for the demonstrations in Washington.</P>
<P>Requiring opponents of intervention to endorse "exclusively nonviolent, negotiated political solutions" flies in the face of reality in Colombia. The main "armed actors" in the country today are the government--backed to the hilt by the U.S.--and right-wing paramilitaries in bed with the military.</P>
<P>These forces have killed three-quarters of the more than 40,000 people who have died in the country's civil war. They murder hundreds of nonviolent trade unionists every year.</P>
<P>On several occasions in the last three decades, guerrilla organizations have agreed to "negotiated political solutions" and laid down their arms to participate in the political system. Each time, paramilitaries have murdered hundreds of the ex-guerrilla militants.</P>
<P>So, for many activists in Colombia, armed self-defense is a matter of survival. Thus, it takes a certain amount of arrogance for a U.S. solidarity group to deny membership to Colombians, while at the same time pronouncing on what they believe is the only viable solution to the struggle in Colombia.</P>
<P>One doesn't have to endorse the politics or tactics of any one group in Colombia to acknowledge their right to defend themselves when death squads or government forces attack them.</P>
<P>Would the leaders of the National Mobilization have made the same demands of Jewish fighters against the Nazis in the Warsaw Ghetto?</P>
<P>Like antiwar activists who feel compelled to emphasize their patriotism, Mobilization leaders seem to think that supporting an exclusively nonviolent strategy in Colombia will stop right-wingers from labeling them "supporters of terrorism."</P>
<P>They are mistaken. If Bush's GOP henchmen are willing to compare Senate Majority Leader Tom Daschle to Saddam Hussein, genuine critics of the U.S. war drive can expect much worse.</P>
<P>A movement that stands up to these slanders and points the finger at the real terrorists--the Colombian government and its U.S. backers--can give confidence to wider layers of people to oppose the U.S. war in Colombia.</P>
<P>But a movement that insists on a pacifist litmus test will remain restricted to the already convinced.</P>

