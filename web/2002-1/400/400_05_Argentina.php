<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ARGENTINA</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Police attack on occupation beaten back</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By H&eacute;ctor Reyes</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 29, 2002 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WORKERS AND supporters defeated an attempt by police to break up an occupation at the Brukman textile plant in Buenos Aires last week.</P>
<P>The protest came less than a week after the first national gathering of neighborhood assemblies (Asambleas Populares Barriales) marched to Brukman in a show of solidarity for the workers.</P>
<P>The heavily armed cops attacked not only the workers, but also their lawyers and other people who tried to mediate. But the police who surrounded the plant gates were soon met by a <I>cacerolazo</I>--a pot-banging protest of 200 workers and others who live in the San Cristobal neighborhood.</P>
<P>Members of three local assemblies blocked the streets, while hundreds more people sounded the alarm from their balconies. An independent socialist member of the National Assembly, Luis Zamora, arrived, as did prominent members of human rights groups.</P>
<P>Humiliated, the authorities had to retreat--and workers triumphantly reoccupied the plant.</P>
<P>Brukman is one of a handful of factories that was taken over and is currently run by workers after it was abandoned by its owners. The occupation, which began during the December rebellion that forced President Fernando de la R&uacute;a from office, is one of the best known of a series of struggles driven by the economic crisis.</P>
<P>Years of austerity policies--demanded by Washington through the International Monetary Fund (IMF)--have led to depression-level unemployment, hunger and, finally, last year's uprising.</P>
<P>All over the country, both employed and unemployed workers have come together in neighborhood assemblies, trying to find an outlet for their anger and ways to win relief measures from the government.</P>
<P>In these struggles, the assemblies have identified with the <I>piqueteros</I>--the militant unemployed workers' movement whose tactics of road blockades and building occupations have spread to an important minority in the union movement and have even inspired sectors of the middle class.</P>
<P>On March 17, efforts to democratically coordinate the actions and demands of the assemblies took a step forward with the first Asamblea Interbarrial Nacional. More than 3,000 people--representing 150 neighborhoods in Buenos Aires, its suburbs and many provinces from the interior of the country--met in the capital.</P>
<P>Among the most important demands agreed on were:</P>
<P>--nonpayment of Argentina's foreign debt and expulsion of the IMF from the country;</P>
<P>--re-nationalization of privatized companies and services;</P>
<P>--repeal of the 13 percent cut in wages for state workers and pension payments;</P>
<P>--a government consisting of the assemblies along with employed workers and the <I>piqueteros</I>.</P>
<P>Although activists from the assemblies and the <I>piqueteros</I> can be counted in hundreds of thousands and have the sympathy of many more, the key to realizing these demands lies in involving the millions of workers who aren't yet part of the movement.</P>
<P>Much of the organized working class is still held back from confronting employers and the government by leaders of the two main wings of the CGT union federation who have ties to the Partido Justicialista of President Eduardo Duhalde.</P>
<P>Actions like the fight at Brukman point the way forward.</P>

