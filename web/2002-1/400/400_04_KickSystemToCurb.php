<P><font face="Times New Roman, Times, serif" size="5"><b>Kick this system to the curb</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 29, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>It's politics as usual here in Massachusetts. First, the legislature approved $30 million in budget cuts. Then, Governor Swift announced she is plugging the gap with other monies. Now, Swift has announced her own round of budget cuts across the board.</P>
<P>I work for an agency that provides services to homeless people. Eventually these cuts will trickle down to my agency and possibly my job.</P>
<P>This highlights the sick irony of a capitalist system: When the economy slows down, tax revenues also slow down, so the politicians respond by cutting programs that help those who are kicked to the curb by a recession.</P>
<P>What are we to do? Keep on fighting the system. Build unity among left and progressive movements. Only then we can kick this system to the curb.</P>
<B><P>Bruce Burleson, </B>Boston</P>

