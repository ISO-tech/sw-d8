<P><font face="Times New Roman, Times, serif" size="4">Two million rally in Italy</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"We'll defend our unions"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 29, 2002 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>YURII COLOMBO reports from Rome on the massive protest by Italian workers.</P>
</B><P>IT WAS the biggest demonstration in Italian history. Two million people, organized by the union federation CGIL, mobilized March 23 to protest a labor law "reform" proposal that would strip unions and workers of their rights.</P>
<P>The red banners of the unions and left-wing political parties were everywhere around the Circus Maximus, site of the chariot races during the Roman Empire. Italy's social forum movement and alternative unions were also in the streets. The unions mobilized for the protest with 9,000 buses, 60 trains, three ships and two planes.</P>
<P>The size of the protest was all the more dramatic because the government tried to use the assassination of a government adviser on labor reform four days earlier to force the unions to cancel the protest.</P>
<P>The so-called Red Brigades--a tiny armed group claiming to act on behalf of the left--claimed responsibility for the murder of Marco Biagi. But this only played into the hands of the government, leading some to suspect the involvement of the Italian secret service (Italy's FBI) in the assassination.</P>
<P>The murder could be used to justify a campaign of repression and intimidation against the most combative union and socialist militants. Just hours after the killing, right-wing Prime Minister Silvio Berlusconi declared that labor unions "feed the inhuman ideology that moves the hand of the killers."</P>
<P>From the platform, union officials and politicians bowed to the pressure by speaking in defense of the "state and democracy" and playing an Italian national hymn that dates from the fascist era.</P>
<P>But in the crowd, the slogans were about confronting the bosses and the government. In fact, the call for a demonstration--and a general strike to follow it--came from the union rank and file after a series of local strikes.</P>
<P>"Today, we showed to the government, bosses and terrorists that they can't stop us," Fabrizio Portaluri, a shop steward at the big Pirelli tire plant in Milan, told <I>Socialist Worker</I>. "We want to defend our rights. Today in Rome, there were many workers who voted for Berlusconi and the right, and now their ideas have changed. Workers are beginning to understand that the problem is political--the problem is Berlusconi's government."</P>
<P>The proposed labor law changes would deny workers the right to appeal an unjust firing to a judge--a gain won in the big union struggles of the late 1960s and early 1970s.</P>
<P>Last weekend's demonstrations followed a huge mobilization of 300,000 in Genoa last July to protest the Group of Eight summit--despite the police killing of protester Carlo Giuliani. A series of large protests against the U.S.-led war in Afghanistan have involved tens of thousands.</P>
<P>Now the government is trying to put the left on the defensive. But workers won't be intimidated. "Tomorrow, we'll begin to organize the general strike, but we also understand that only one general strike can't beat the government," Portaluri said. "We must think about how to continue the struggle."</P>

