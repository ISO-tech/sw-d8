<P><font face="Times New Roman, Times, serif" size="5"><b>Defend civil liberties, free Rabih Haddad!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 29, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--More than 300 people turned out on March 22 and 23 to protest the detention of Muslims and Arabs and the racist scapegoating that has occurred since September 11.</P>
<P>At panel discussions in Bridgeview, Ill., and Chicago, community members and activists gathered to hear about the case of Rabih Haddad, the founder of an Islamic charity called the Global Relief Foundation (GRF), who was imprisoned December 14. The government claims that Rabih Haddad and GRF have ties to "terrorists," yet there is no evidence--or even any formal charge--against Haddad.</P>
<P>Rabih's brother, Mazen Haddad, told the crowd about the agony Rabih's imprisonment has caused his family--and asked supporters to widen the struggle for his freedom. "Even though we are bringing Rabih Haddad's case to the light, there are hundreds, if not thousands in a similar situation," he told the crowd.</P>
<P>Sami al-Arian, a computer science professor at the University of South Florida, also spoke on the panel. Al-Arian was fired in December after Fox news blowhard Bill O'Reilly branded him a "terrorist" for defending Palestinian rights.</P>
<P>"This is an orchestrated campaign by people who would like to settle political scores, and they found an opening after September 11," said al-Arian. "We need to organize and mobilize for a new civil rights movement."</P>

