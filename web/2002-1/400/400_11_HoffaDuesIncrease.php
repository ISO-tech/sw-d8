<P><font face="Times New Roman, Times, serif" size="5"><b>Hoffa pushes dues increase</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By a member of Teamsters Local 722</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 29, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>BY NOW many Teamsters have seen the fairy tale "Report of the Blue Ribbon Commission on Teamster Finances." As with most publications from the International, you have probably thrown it out by now as being unbelievable--and it is, by the way.</P>
<P>The bottom line is that the IBT wants an additional average of $8 per member per month. What this "report" doesn't tell you is the massive waste of money our union already gets away with. This includes 140 multiple salaries for officers and million-dollar parties all over the country, just to name two areas.</P>
<P>In addition to the normal per capita dues sent up to the International, they've been getting an extra $1 per member per month emergency assessment under Article 10, Section 3 of the IBT Constitution. The International already gets more than $82 million dollars per year in income. And the Teamsters are a "bottom-up" organization with the locals doing all the work.</P>
<P>Considering that a local union has to pay for most all its member services, what does the International do for the local members with that $82 million? The answer is very little to nothing at all.</P>
<P>In 1994, Hoffa opposed a dues increase to build the strike fund. Then in 1996 he pledged to immediately raise strike benefits without a dues increase.</P>
<P>Hoffa and the rest of the General Executive Board are smart enough not to let the members have a direct vote on this dues increase because it would be voted down for the waste of money it is.</P>
<P>Instead, the IBT will hold a "special convention" in Las Vegas and the IBT delegates (read: officers) will vote for you. Considerate of them, isn't it?</P>
<P>Teamster rank-and-filers must contact their local unions and demand a direct vote on this issue.</P>

