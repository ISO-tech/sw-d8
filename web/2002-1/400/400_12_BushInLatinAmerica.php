<P><font face="Times New Roman, Times, serif" size="5"><b>Bush faces protests on Mexico trip</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Todd Chretien</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 29, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THOUSANDS OF police fired tear-gas canisters at protesters to clear the way for George W. Bush's visits to Mexico, Peru and El Salvador last week.</P>
<P>Bush's handlers hoped that his speech at a United Nations conference on world poverty in Monterrey, Mexico, would be a platform to show off America's financial aid "carrot" to poor countries--to soften up growing opposition to the "big stick" of military aid to the region's most brutal regimes.</P>
<P>But no one was buying it. And you can hardly blame them--because Bush wasn't exactly generous.</P>
<P>He announced that he would ask Congress to authorize a measly $5 billion in aid to poor countries--starting in 2004 and spread over three years. But even that delayed drop in the bucket comes with strings attached.</P>
<P>"Developed nations have a duty to not only share our wealth," Bush lectured. "We must tie greater aid to political and legal and economic reforms&#133;And by insisting on reform, we do the work of compassion."</P>
<P>What a joke! If Bush cared about "compassion," he could start by redirecting just a fraction of the money spent on bombing Afghanistan toward emergency food relief for the estimated 250,000 to 500,000 who face starvation in Guatemala and El Salvador because of a terrible drought.</P>
<P>Or he could demand the release of U.S. citizen Lori Berenson, serving a 20-year sentence in a Peruvian jail on trumped-up charges of aiding rebels who opposed now-deposed dictator Alberto Fujimori.</P>
<P>Instead, Bush had a different message: Free trade and the free market will solve all problems--and we'll give you guns to repress anyone who thinks otherwise.</P>
<P>The U.S. government already has military bases in Ecuador and Bolivia, along with hundreds of Pentagon "advisers" in Colombia, and it sells hundreds of millions in military hardware to Chile, Argentina and Brazil.</P>
<P>But Bush wants more. He wants to return to the Cold War era, when the U.S. openly organized the Contra death squads that murdered 50,000 in Nicaragua; propped up a genocidal military regime in Guatemala; toasted the military dictators of Chile, Brazil and Argentina as great humanitarians; and treated Honduras as a giant aircraft carrier.</P>
<P>U.S. intervention to "do the work of compassion" then meant propping up a bunch of military dictators--who cracked down on all resistance in order to pave the way for years of neoliberal robbery.</P>
<P>We have to organize to keep the U.S. government from causing more disasters in Latin America.</P>

