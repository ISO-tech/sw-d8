<P><font face="Times New Roman, Times, serif" size="4">New exercises to be met by protests</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>U.S. Navy out of Vieques!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Roberto Barreto</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 29, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ORGANIZATIONS FIGHTING to kick the U.S. Navy off the Puerto Rican island of Vieques are getting ready for a campaign of resistance after Navy officials announced that three weeks of bombing exercises would begin April 1.</P>
<P>Several groups are calling for new civil disobedience actions on Vieques, where hundreds of people have taken action at the Navy's bombing range in the past. What's more, organizations are planning acts of resistance on the main island, in the capital city of San Juan.</P>
<P>For example, the Comit&eacute; Amplio Contra la Guerra--an antiwar committee based in San Juan--is organizing protests on Puerto Rico's main highways for April 1. Another target for demonstrations is the U.S. Federal Court building--to highlight the stepped-up repression against activists fighting to oust the Navy.</P>
<P>On March 22, more than 100 people protested the sentencing of student Pedro Col&oacute;n Almenas to a one-year jail term because of his role in a protest against the Reserve Officers' Training Corps at the R&iacute;o Piedras campus of the University of Puerto Rico (UPR).</P>
<P>Now a federal grand jury is secretly investigating other students. Using provisions of the recently passed USA PATRIOT Act, prosecutors have demanded that three campuses from the UPR system hand over the confidential records of students--so authorities can identify activists.</P>
<P>Meanwhile, the Navy's bombardment on Vieques is taking a heavy toll. Activists say that Navy exercises last October caused a 240 percent increase in radioactivity in the Vieques neighborhood of Esperenza.</P>
<P>Hundreds of Vieques residents suffer from multiple illnesses connected to the bombing exercises. On February 26, the Committee for the Rescue and Development of Vieques reported that six island residents had died in a single weekend, four of them from cancer.</P>
<P>Besides the poisonous effects of the Navy's target practice on Vieques residents, the bombing range is used to train U.S. troops for imperialist interventions--another important reason why the exercises should be stopped.</P>
<P>Activists from the U.S. and around the world should support the people of Vieques--and demand that the U.S. Navy get out now!</P>

