<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Why did Arafat agree to an unjust peace?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 29, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>FOR DECADES, the Palestinian struggle for national liberation inspired people committed to justice around the world. The fight against Israel--a Western-backed state founded in 1948 by driving Palestinians off their land and expanded in 1967 by the conquest of parts of neighboring Arab countries--was seen as an important part of an international struggle against imperialism and colonialism.</P>
<P>As head of the Palestine Liberation Organization (PLO), Yasser Arafat was acknowledged as the determined leader of the struggle. But today, Arafat seems a shadow of his former self--asking permission from the Israeli military before he travels and begging forgiveness for every act of resistance by Palestinians.</P>
<P>How did it come to this? Why is Arafat constantly extending the hand of cooperation to Israel, only to have it slapped away?</P>
<P>Part of the answer is the position that Arafat occupies today. He is no longer the leader of a guerrilla struggle--but head of the Palestinian Authority, the mini-state established under the Oslo peace accords that Arafat negotiated with Israeli officials in 1993.</P>
<P>Oslo was never meant to give Palestinians a viable state. Instead, the agreement left Palestinians in a subordinate position, requiring the PLO to renounce violence, without requiring the same of Israel, despite its overwhelming military superiority.</P>
<P>"For the first time in our history, our leadership had simply given up on self-determination, Jerusalem and the refugees, allowing them to become part of an undetermined set of 'final status negotiations,'" wrote well-known Palestinian intellectual Edward Said in the mid-1990s. "For the first time in the 20th century, an anti-colonial liberation movement had not only discarded its own considerable achievements but made an agreement to cooperate with a military occupation before that occupation had ended."</P>
<P>In exchange for these concessions, Israel and its U.S. backers were offering very little--as American officials essentially admitted at the time. "If autonomy does not improve security for Israel, there will be no Palestinian autonomy," said James Baker, the former Secretary of State.</P>
<P>But Oslo alone doesn't explain Arafat's paralysis today. It's also important to remember that the picture of Arafat as a determined revolutionary never fit reality.</P>
<P>For decades, Arafat's Fatah faction of the PLO focused on a top-down strategy--trying to pressure the rulers of neighboring Arab countries to fight for Palestine's liberation. But the Arab regimes didn't want to jeopardize their working relationships with U.S. imperialism.</P>
<P>So, again and again, Arab governments not only failed to respond to the call for a struggle against Israel, but turned on the PLO, driving it out of their territories. In the best-known instance, King Hussein of Jordan expelled the PLO in 1970--in large part because of the PLO's broad popularity among Jordanian workers.</P>
<P>Had the PLO led a rebellion in Jordan, it would have touched off a crisis across the Middle East and brought Arab working classes across the region into a direct confrontation with bankrupt Arab regimes.</P>
<P>But after its expulsion, Fatah drew the opposite conclusion--that threatening the Jordanian regime was an unnecessary provocation. "The extremists jumbled together the fight for national liberation (which Fatah advocated exclusively) with the class struggle," said Salah Khalaf, one of Fatah's founders.</P>
<P>In fact, this is the only practical approach to the struggle. Defeating Israel's military superiority--backed up by the U.S.--will require a struggle from below that fuses the Palestinian demand for self-determination with the grievances of workers across the region against their own ruling classes.</P>
<P>In rejecting this conclusion, Arafat has few alternatives but to pursue negotiations, even as Israel commits daily atrocities against Palestinians. "Peace" agreements--whether backed by Israel, the U.S. or Arab leaders--won't bring peace as long as the essential injustice at the heart of the conflict remains.</P>

