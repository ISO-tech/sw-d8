<P><font face="Times New Roman, Times, serif" size="5"><b>Laidlaw Bus</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Steve Leigh</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 29, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SEATTLE--Chanting "Fight! Fight ! Fight!" some 400 bus drivers, mechanics and supporters rallied to defend their jobs March 19.</P>
<P>The school board proposed to take away the bus contract from Laidlaw, organized by Teamsters Local 763, and give it to nonunion companies that came in with a lower bid. More than 350 drivers and mechanics would lose their jobs--many with 20 to 30 years experience--if the board gets its way.</P>
<P>After rallying at the Teamster Hall, they marched to the school district headquarters and planted stakes in the lawn with names of drivers and mechanics and their years of service.</P>
<P>Speakers stressed both safety and union busting. "The link between inexperienced drivers and children's safety cannot be underestimated," said 763 organizer Sarah Luthens. "The top 200 drivers for Laidlaw average over 13 years of experience. The nonunion companies rely mostly on novice drivers. Those school board members who own a $30,000 SUV wouldn't loan it out to inexperienced drivers, but they would entrust school children to them?"</P>
<P>"We're becoming part of the race to the bottom," said Jeff, a driver. Other speakers delivered solidarity messages from Jobs with Justice, the International Longshore and Warehouse Union and the Amalgamated Transit Union.</P>
<P>The next day, the school board phones were jammed with supporters of the Laidlaw workers. Dozens came to the school board meeting and got the school board to postpone its decision for at least two weeks, giving the union supporters a chance for further organizing.</P>

