<P><font face="Times New Roman, Times, serif" size="5"><b>Cal State antiwar activists gain confidence</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 29, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>Recently, antiwar students at Cal State-Long Beach won two victories. In an 8-to-5 vote, the Student Senate passed a resolution that read, "The USA PATRIOT Act allows the federal government to procure students' records from university administrations without the students' knowledge or consent. We the undersigned demand that President Maxson and his administration does not cooperate with this law."</P>
<P>Our antiwar coalition petitioned and collected hundreds of signatures in support of the resolution. It was because of our organizing that the resolution passed, and we hope the idea spreads to other schools.</P>
<P>Also, Turkey's ambassador to the U.S. recently showed up in Los Angeles to give a speech on our campus. He talked about how useful Turkey is as an ally in the U.S. "war on terrorism." It was disgusting.</P>
<P>We tried to bring in signs that read "Stop Killing Kurds" and "Free the Political Prisoners," but we were told by campus police that the "free speech area is outside." If we were going to have signs, we were going to be kicked out.</P>
<P>Luckily, we had photocopied pages from a <I>Socialist Worker</I> pamphlet on Kurdistan, which we passed out--until security told us that we were not allowed to distribute literature.</P>
<P>During the question-and-answer period, the ambassador got grilled by Armenian students and antiwar activists. He left embarrassed, after only two out of the 20 or so questions that he fielded were friendly.</P>
<P>We left feeling much more confident. We are going to continue organizing, because we know that there are many students out there who are fed up with the violations of our rights and the killing of innocent people in the "war on terrorism."</P>
<B><P>Sarah Knopp,</B> Long Beach, Calif.</P>

