<P><font face="Arial, Helvetica, sans-serif" size="2"><b>CHINA</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Workers defy repression to fight layoffs</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Rebellion against the free market</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 29, 2002 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHINESE WORKERS in two northern cities are organizing the largest protests since the Tiananmen Square democracy demonstrations of 1989.</P>
<P>At the oil refinery complex run by PetroChina Co. in Daqing, thousands of workers have surrounded company headquarters in daily protests since early March. They're protesting layoffs of tens of thousands of workers--and management's cutoff of payments for heating bills and health insurance for workers who received severance packages of less than $500 for each year worked. The workers were also demonstrating against corrupt bosses who cashed in when the complex was sold off in 1999.</P>
<P>Meanwhile, in the industrial city of Liaoyang, protests over similar issues have involved as many as 50,000 workers from six different factories. Workers from the bankrupt and closed Ferro-Alloy factory led the protests, demanding back pay and benefits and an investigation of managers who embezzled pension funds.</P>
<P>The size of the demonstrations kept police at bay for weeks. But police attacked the Liaoyang protests on March 19, arresting Yao Fuxin, who is accused of leading the protests, along with several other workers.</P>
<P>The government paid 1,400 workers half the money owed to them, with promises of more later.</P>
<P>Michael Lev, the <I>Chicago Tribune</I>'s correspondent in China, who visited Liaoyang during the protests, told <I>Socialist Worker</I> that the workers "feel lost and abused. Their complaints are mainly economic, but having worked for state-run enterprises, the boss they hate is also a government official, and instead of complaining of being 'cheated,' they complain of 'corruption,' so that makes it political."</P>
<P>The demonstrations have exposed the lie that China is a socialist society. After the revolution of 1949, the Chinese Communist Party under Mao looked to Stalin's dictatorship in Russia as a model for a state capitalist system, built around a network of heavy industries that promised jobs for life.</P>
<P>But for the last two decades, the Communist Party has turned to the free market and Western investment--calling it "socialism with Chinese characteristics."</P>
<P>With the economy booming, millions of poor peasants flooded into boomtowns near Hong Kong to work in factories owned by Western multinationals like Boeing, Nike and General Motors, as well as by Communist Party bosses and military chiefs.</P>
<P>A minority of the Chinese elite accumulated staggering wealth. Meanwhile, old state-run industries in cities like Liaoyang and Daqing have been in long-term decline. "Everybody talks about China's miracle," one worker told a <I>Washington Post</I> reporter. "Well, there are no miracles here."</P>
<P>At the same time, even the rapidly growing new industries can't absorb all the desperately poor peasants flooding into the cities to find work. At least 100 million people are unemployed.</P>
<P>"The government is allowing the old system to fade away, and it is calculating that the new economy will over time be able to provide jobs for everyone," Lev said. "But the transition is a long one, and there will be many winners and losers."</P>
<P>Since the army massacre that smashed the Tiananmen Square protests in 1989, the government has ruthlessly suppressed dissent, including the Falun Gong religious group. But the big protests in recent weeks show that workers have the collective power to defy the crackdown.</P>

