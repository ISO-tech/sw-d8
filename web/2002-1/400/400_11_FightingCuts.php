<P><font face="Times New Roman, Times, serif" size="5"><b>Fight for our schools</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 29, 2002 | Page 11</font></p>

<p><font face="Times New Roman, Times, serif" size="4"><b>Berkeley High School</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jean Whittlesey, Berkeley Federation of Teachers</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>BERKELEY, Calif.--About 300 Berkeley High School students walked out of class March 6 to protest budget cuts.</P>
<P>The Berkeley Unified School District cut $3.8 million from its budget in its first attempt to make up for a shortfall of $6 million.</P>
<P>Chanting "We will not go quietly into the night, we will not give up without a fight," students confronted the new school superintendent Michelle Lawrence on the district headquarter steps.</P>
<P>The cuts will restructure the school day into six instead of seven periods, meaning fewer elective choices such as African American studies, music and art.</p>
<p>The esteemed science program will be cut in half, and 173 temporary and probationary teachers received pink slips March 6 and 7. This will mean larger class sizes, teachers being moved within the district to fill vacancies and further cutting of electives.</P>
<P>Students and teachers need to stand together against the Berkeley budget cutters.</P>

