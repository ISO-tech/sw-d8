<p><font face="Times New Roman, Times, serif" size="4"><b>Stop the cuts at CUNY</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Wengraf</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--City University of New York (CUNY) students are gearing up to fight a new round of budget cuts.</P>
<P>About 50 demonstrators from Jobs with Justice, Student Liberation Action Movement, Al Sharpton's National Action Network and Borough of Manhattan Community College students and professors gathered at New York's City Hall March 22 to protest Gov. George Pataki's proposed cuts to the Tuition Assistance Plan (TAP). The plan will raise student costs by $2,000 over four years.</P>
<P>CUNY faculty had been working without a contract since August 2000, and reached a tentative agreement in early March--providing for a 9 percent increase in pay and benefits. But protesters demanded that raises for faculty not come at the expense of more costs for students.</P>
<P>When Mayor Mike Bloomberg happened to arrive during the protest, demonstrators surrounded him and demanded to know what steps he would take for CUNY. Scurrying up the steps, all the billionaire mayor could say was that his hands were tied on the state's budget.</P>
<P>We need to keep up the pressure so politicians like Bloomberg can't hide from the impact of their cuts.</P>

