<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Sharon wants to drown the Palestinian Intifada in blood</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Israel's relentless war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 29, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ISRAELI PRIME Minister Ariel Sharon claims the right to decide whether Yasser Arafat can attend an international conference--and whether he'll be able to return to his home afterwards.</P>
<P>If this happened to an Israeli diplomat, there'd be an international outcry. If it happened to an American political leader, the bombers might already be in the air.</P>
<P>But as far as the U.S. government is concerned, there's no limit to the humiliation that Israel can heap on Arafat. When Israel acts like a colonial overlord, the U.S. blames the victim.</P>
<P>Last week, George W. Bush mouthed the tired refrain that the chair of the Palestinian Authority (PA) "must do more to stop the violence." As if Palestinian suicide bombings and stone throwing could ever match Israel's systemic campaign of assassinations, invasions, mass detentions and closures of towns and refugee camps.</P>
<P>In fact, even as this week's Arab League summit discusses a Saudi Arabian peace plan, press reports revealed that Israeli military planners are preparing for their most aggressive assault yet. "[T]here is widespread and growing support, both in [Sharon's] government and in the army, for what one official called a 'comprehensive military confrontation' with the Palestinians," reported the <I>Washington Post.</P>
</I><P>The summit of Arab leaders will consider the Saudi peace deal proposed last month that would offer "full normalization" of relations between Arab nations and Israel in exchange for Israel's withdrawal to its 1967 borders.</P>
<P>The plan ignores key issues such as the right of return for Palestinians forced off their land by Israel's wars of aggression. Nevertheless, Arafat's PA backs it, and the Bush administration says it's a welcome step.</P>
<P>But Sharon will never contemplate the territorial concessions contained in the plan. Sharon himself is the architect of a plan to move Jewish "settlers" into territory occupied by Israel since its 1967 war against neighboring Arab countries.</P>
<P>And since his election in February last year, he has continued building on this legacy, establishing 34 new settlements in the West Bank and Gaza, according to report by the Israeli group Peace Now.</P>
<P>Meanwhile, the Israeli military's maze of checkpoints has expanded massively since the Palestinian uprising--or Intifada--against the Israeli occupation began 18 months ago. The 30-mile trip from Hebron to Ramallah is a 10-hour journey, full of frustration and danger--considering how frequently Palestinians are shot and killed at the checkpoints.</P>
<P>"This is not a life," Khalil Amr told a reporter, as he trudged along dirt paths turned to mud by the rain. "Dogs live better than this."</P>
<P>Sharon, of course, has been open about his strategy of drowning the Intifada in blood. "If the Palestinians are not being beaten, there will be no negotiations," he told reporters March 4. "The aim is to increase the number of losses on the other side. Only after they've been battered will we be able to conduct talks."</P>
<P>So don't believe for a second the claims of Israeli officials that they are bargaining in good faith during the visit of U.S. envoy Gen. Anthony Zinni.</P>
<P>Over the decades, the U.S. has given more than $80 billion in aid to Israel, and it has also vetoed no less than 29 United Nations Security Council resolutions condemning Israel for its illegal settlements and other violations of international law.</P>
<P>That's a huge investment in America's firmest ally in the oil-rich Middle East--and U.S. officials aren't going to let human rights abuses or breaches of international law weaken their support.</P>
<P>That's why those who stand for justice in the U.S. need to support the Palestinian resistance to oppression--and build a movement that can force the U.S. to cut its ties to Israel's racist and brutal regime.</P>

