<P><font face="Times New Roman, Times, serif" size="5"><b>Vicious political game of India's right wing</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 15, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>My family in India lives in Ahmedabad, the city in Gujarat that bore the brunt of recent Hindu-Muslim violence. Less than a mile away from their home, Muslim stores and homes were torched and innocent people butchered in attacks launched by right-wing Hindu organizations.</P>
<P>On the surface, the anti-Muslim violence was a response to a vicious attack on February 27, in which Muslims from the town of Godhra set a train carrying Hindu pilgrims on fire, killing 58.</P>
<P>But the attacks and counterattacks are in the context of Hindu fundamentalists' assault on and scapegoating of the Muslim minority.</P>
<P>The "pilgrims" were coming from Ayodhya, the site where Hindu militants demolished the Babri Mosque in 1992, unleashing months of violence in which more than 3,000 Muslims were killed. The Hindu right has vowed to rebuild a temple on that same spot by March 15, in defiance of the country's Supreme Court.</P>
<P>The government, controlled by the far-right BJP--the political arm of Hindu militants--was conveniently slow to respond. Recent atrocities are forcing them to act, but often, army soldiers and police look on as the rioters continue their deadly work--even chopping people up and burning them alive.</P>
<P>I was surprised to hear my uncle--who votes for the BJP, like most Hindus in Gujarat--voice that opinion. "They're just playing a political game," he said. "They delayed sending the army so the thugs could have their fun."</P>
<P>Between the Gujarat earthquakes of last year, in which corruption in building contracts amplified the damage to human life, and the current riots, we're getting really sick of the violence of this system.</P>
<P>Hopefully, the senseless killings will awaken more Hindus to the false promise of the Hindu right.</P>
<B><P>Pranav Jani,</B> New York City</P>

