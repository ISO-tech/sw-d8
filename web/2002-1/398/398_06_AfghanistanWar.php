<P><font face="Times New Roman, Times, serif" size="5"><b>Raining more death on Afghanistan</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 15, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>NICOLE COLSON reports on the endless U.S. war on Afghanistan.</P>
</B><P>SINCE THE end of last year, Bush and Co. have gloated daily over the U.S. "liberation" of Afghanistan. But last week, the Pentagon was trying to explain why U.S. forces were suddenly fighting their largest battle in Afghanistan to date.</P>
<P>The administration's supposed "victory" evaporated into an intense U.S. assault on eastern Afghanistan near Gardez, with the Pentagon scrambling to rush hundreds of troops and dozens of attack helicopters to the area.</P>
<P>The U.S. media focused obsessively on the handful of U.S. casualties. But that masked the slaughter that the U.S. carried out. "In the last 24 hours, we have killed lots, lots of al-Qaeda and Taliban, I won't give you precise numbers, but we've got confirmed kills in the hundreds," gloated Maj. Gen. Frank Hagenback.</P>
<P>Hardly surprising, considering the lengths that the Pentagon was willing to go to. For the first time ever, the U.S. used a "thermobaric" bomb, which has the force of a low-level nuclear weapon and kills by tearing people apart from the inside out--then incinerating them.</P>
<P>And if innocent people are killed along the way, the Pentagon thinks that's tough. "We might have killed noncombatants," sniffed U.S. military spokesperson Maj. Bryan Hilfery. "But they certainly went in there knowing what they were going into."</P>
<P>For most ordinary Afghans, there's been nothing to celebrate about the U.S. "victory." In fact, the new Afghanistan looks a lot like the old one.</P>
<P>In February, CIA officials warned that factional divisions between rival warlords were threatening to plunge the country into civil war. But as one journalist put it, "Afghanistan is not in danger of disintegrating into civil war. This is civil war."</P>
<P>Some warlords have even used the U.S. to do their dirty work. In January, U.S. Special Forces stormed the village of Hazar Qadam in southern Afghanistan, killing 21 people and taking 27 prisoner. After more than a month of repeated denials, the CIA finally admitted that it had been duped by a warlord who used his U.S.-supplied satellite phone to call in the raid on his rival.</P>
<P>Elsewhere in the country, there are new and horrifying reports of ethnic violence. In northern Afghanistan, ethnic Pashtuns were forced to flee for their lives from town after town in the face of a campaign of mass rapes and looting organized by Uzbek warlords who took over when the Taliban was driven out.</P>
<P>"It has been systematic and wide-scale," a United Nations official told the <I>New York Times. </I>"Rapes are far more common than killings, but the serious looting is very pronounced. With the change in power, it is time to settle old scores."</P>
<P>Also, the famine that humanitarian groups warned about at the beginning of the war has become a reality for many Afghans--especially those living in rural areas. Aid workers say that there are pockets of starving people across the country. And with the renewed threat of robbery by armed gangs, deliveries of food can't even get to some remote areas to deliver food.</P>
<P>Some parents have been forced to make horrible sacrifices. When he ran out of food to feed his 10 children, Akhtar Muhammad started by selling his farm animals, then his furnishings and even wood beams from his house, the <I>New York Times</I> reported.</P>
<P>But when there was nothing left and his children were beginning to starve, he took his 10-year-old and 5-year-old sons to the local bazaar--and, like other desperate parents, sold them.</P>
<P>In exchange for his two sons, Muhammad will receive 79 pounds of wheat each month for the next six years. The children will work as indentured servants during that time. "Sometimes I still cry," said Muhammad's 10-year-old son Sher, whose new "owner" regularly beats him. "I cry at night. But I understand why the selling of me was necessary."</P>
<P>And George W. Bush calls this a "victory." War, violence, poverty, famine--these are the real legacies of the U.S. war in Afghanistan.</P>

