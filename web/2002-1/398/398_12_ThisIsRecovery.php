<P><font face="Times New Roman, Times, serif" size="4">Bosses still cutting jobs</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>They call this a recovery?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 15, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"AN ECONOMIC expansion is already well underway." So Federal Reserve Chair Alan Greenspan declared during congressional testimony March 7. Greenspan should try out that line on the 22,000 Kmart workers who found out the very next day that they would be losing their jobs.</P>
<P>Kmart management announced that it was closing 284 stores across the country as part of its bankruptcy reorganization--slashing 22,000 jobs at a single stroke.</P>
<P>At a Chicago North Side store slated for closure, workers were angry. "They told us that if we talked to the press, we'd be fired immediately," said one worker. "You couldn't pay me enough to work here again."</P>
<P>What's Kmart so scared of? Maybe that someone might point out the hypocrisy of the company firing 9 percent of its workforce--most of whom make just $6.50 an hour--when a bankruptcy court okayed $150 million in bonuses for management only a few days before.</P>
<P>Kmart claimed that it needed the bonuses to keep valuable executives from jumping ship to other retailers. Even though the cash for bonuses could pay a year's worth of full-time wages for <I>more than half</I> of the workers laid off last week.</P>
<P>But some Wall Street analysts think Kmart was too gentle. "We would be more comfortable with the Kmart story were the company to close more than 300 stores," sniffed Shelly Hale, an analyst with Banc of America Securities, in a note to investors days before the layoff announcement. "We believe that for Kmart to operate as an efficient retailer the company should close at least 500 stores, or closer to 25 percent of its stores."</P>
<P>The job cuts show the other side of the story that Wall Street and the mainstream media avoided last week in their celebration of new statistics that show the economy possibly pulling out of recession.</P>
<P>In particular, the press highlighted a drop in unemployment, the first in nearly a year. But while Wall Street parties, workers had better watch their backs. "Job gains in coming months are likely to be sluggish, despite relatively robust economic growth, as companies continue to restructure," admitted Bruce Steinberg, chief economist at Merrill Lynch &amp; Co. "We still believe unemployment will edge higher in coming months."</P>
<P>In other words, even if the economy turns around, companies will squeeze as much as they can out of their existing workforces.</P>
<P>Meanwhile, for victims of the layoff ax over the past year, it still feels like a recession. According to the Center on Budget and Policy Priorities, some 356,000 workers ran out of unemployment benefits in January.</P>
<P>And of course, those statistics don't apply to the millions of hidden part-time workers--like so many at Kmart--who aren't even eligible for jobless benefits to begin with. "This is the third retail job I've been laid off from this year," a worker at Kmart told <I>Socialist Worker</I>. "It's not just here, it's at so many places."</P>
<P>Corporate America is trying to make workers pay for the recession. It's time to organize the fight to make <I>them</I> pay.</P>

