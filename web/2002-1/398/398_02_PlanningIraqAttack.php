<P><font face="Times New Roman, Times, serif" size="4">U.S. releases new "evidence"</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Hot air to justify a war on Iraq</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 15, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>DICK CHENEY and the Bush gang want a war on Iraq, and they'll say just about anything to get their way.</P>
<P>With Cheney packing for this week's visit to the Middle East, the White House must have been nervous about United Nations General Secretary Kofi Annan's meeting last week with Iraqi officials to discuss a proposal for new international weapons inspection teams.</P>
<P>A UN agreement on inspections to confirm that Iraq isn't building so-called "weapons of mass destruction" could spoil a perfectly good excuse for war. So on the eve of Annan's meeting, U.S. officials released "evidence" of an Iraqi "military buildup"--involving trucks bought through the UN's "oil-for-food" program, no less.</P>
<P>The "evidence" is satellite photos that supposedly show "dump trucks that were stripped and diverted for possible usage in air defense and missile systems," an anonymous U.S. official told Britain's <I>Guardian</I> newspaper.</P>
<P>Even right-wingers like former UN weapons inspector Scott Ritter weren't buying it. "The U.S. wants to undermine [Annan's meeting], pure and simple," Ritter said.</P>
<P>Implicating the UN's "oil-for-food" program as part of the alleged military buildup is especially cynical. The program was supposed to soften the savage impact of UN sanctions on Iraq, in place since the 1991 Gulf War. More than 1 million Iraqis have died because of the sanctions.</P>
<P>Under the oil-for-food deal, Iraq is allowed to sell a limited amount of oil and use the revenues to purchase food, medical supplies and other basic necessities--after covering the UN's expenses in administering the program.</P>
<P>Former UN officials like Denis Halliday--who quit his job as coordinator of humanitarian aid for Iraq in protest against the sanctions--say that the oil-for-food program is a drop in the bucket and that all sanctions should be lifted. But the U.S. government and its lapdog British allies have insisted on maintaining sanctions.</P>
<P>The UN's embargo was coming under increasing criticism before September 11. But the U.S. government is tightening the noose. "There's a lot of hot air right now," Halliday told <I>Socialist Worker,</I> "and it's used as an excuse to justify this war on terrorism."</P>
<P>To kick off his Middle East mission, Cheney stopped in London to give British Prime Minister Tony Blair his marching orders: Draw up plans for sending 25,000 troops to participate in an invasion force to topple Saddam Hussein.</P>
<P>The message is plain: The U.S. government won't be satisfied with anything short of total war.</P>

