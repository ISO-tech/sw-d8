<P><font face="Times New Roman, Times, serif" size="5"><b>Verizon</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By a member of CWA 1106</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 15, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Members of Communications Workers of America (CWA) Local 1106 at Verizon in Queens have begun to organize against a speedup.</P>
<P>Management's plan--called the Service Experience Plan (SEP)--will result in suspensions and firings by setting quotas for jobs per day for technicians. The company has wanted this for years, but never succeeded in forcing it on the union.</P>
<P>Ever since our 2000 strike victory, Verizon's management has been on the offensive in an attempt to turn back the clock. They claim the plan will increase "quality" and "productivity," but no plan can do both simultaneously.</P>
<P>Technicians with 15 years on the job have been sent back to school, which is pure humiliation--it's the broken-down facilities that slow us down, not lack of knowledge.</P>
<P>Members have grown nervous as suspensions draw near, and no clear plan of action has come from the union. With our union meeting approaching, a group of us began discussing what we could do to respond.</P>
<P>Our strongest response would be to walk out--but we're just one section of the workforce in New York and could be replaced if other locals didn't honor our walkout.</P>
<P>Gathering a night before our meeting, we drew up a list of problems and actions to raise at the meeting. While the leadership largely outmaneuvered us in the meeting, they were forced to call for action, planning job actions over the weekend.</P>
<P>In response, the company increased the number of people working on Sunday--and had to pay them double time!</P>
<P>This success belongs to the membership and must be used to push for escalating the fight even further. We are discussing how to keep the pressure up through pickets, rallies and other actions.</P>

