<p><font face="Arial, Helvetica, sans-serif" size="2">March 15, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THINGS ARE getting bad at the Justice Department these days. Not only does Attorney General John Ashcroft make his staff endure a daily prayer meeting, but he's also making them participate in sing-alongs.</P>
<P>A group of Hispanic Justice Department employees recently were summoned to Ashcroft's office, thinking that perhaps their boss was attempting to open a discussion about how to make the Justice Department more diverse.</P>
<P>Instead, Ashcroft handed them copies of a patriotic song he had written called "Let the Eagle Soar"--and commanded them to translate it into Spanish.</P>
<P>Ashcroft is so proud of his "musical achievement" that some Justice Department staffers are complaining that he hands out copies at meetings and strong-arms employees into singing along.</P>
<P>"Have you heard the song?" one employee asked a reporter. "It really sucks."</P>
