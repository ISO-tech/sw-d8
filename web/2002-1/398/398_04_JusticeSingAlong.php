<P><font face="Times New Roman, Times, serif" size="4"><b>The happiest military on earth</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>HOW DO you make over your image when you have the worst human rights record of any military in the Western hemisphere? Get an inflatable mascot!</P>
<P>The Colombian military--which is currently in the process of bombing a 16,000-mile zone in the southern part of the country--is concerned that citizens don't see it as "friendly enough."</P>
<P>So the army recently revealed its new 10-foot-tall inflatable mascot--a soldier dressed in military fatigues. The giant grinning figure has a real soldier inside--whose job is to go around hugging civilians to get them to "trust" the military.</P>
<P>"Remember, your army loves you," the cartoonish character told townspeople as they waited to cross a military checkpoint. "Join in by denouncing these bandits who are killing this country."</P>
<P>--Reuters, February 27, 2002</P>

