<P><font face="Times New Roman, Times, serif" size="5"><b>A call for ending bigotry against gays</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 15, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>TELEVISION: <I>The Matthew Shepard Story,</B></I> an NBC television movie starring Stockard Channing, Sam Waterston and Shane Meier. Showing March 16.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>IN OCTOBER 1998, 21-year-old gay college student Matthew Shepard was viciously beaten and left to die, tied to a fence in Laramie, Wyoming. Shepard's murder sent shock waves across the country, as millions of people were faced with the deadly outcome of antigay bigotry.</P>
<P>NBC's <I>The Matthew Shepard Story</I> concentrates on Matthew's family as they come to terms with their son's homosexuality in conservative Wyoming and with the horrific brutality of his murder.</P>
<P>A scene of Matthew's funeral depicts a sickening protest led by bigot Rev. Fred Phelps, his followers carrying signs that read "God Hates Fags." This experience and others made Matthew's mother, Judy, into an activist, starting a foundation to educate others about tolerance. And, as the movie shows, it transformed her opinion about whether executing Matthew's murderers would bring justice for her son.</P>
<P>Since the story focuses almost exclusively on Matthew's personal life and those of his family, viewers miss out on the important shift in public consciousness that came after Matthew's death. This included a series of protests, not the least of which included high school football players in Laramie wearing symbols to honor Shepard.</P>
<P>The movie's script isn't great. But it's a welcome sight when network television presents firm support for gay and lesbian rights to a mainstream audience--not to mention opposition to the death penalty.</P>

