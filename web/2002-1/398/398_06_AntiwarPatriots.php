<P><font face="Times New Roman, Times, serif" size="5"><b>Are antiwar activists the "real patriots"?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 15, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>ALAN MAASS argues that activists should be unpatriotic--and proud of it.</P>
</B><P>"THE 'WAR on Terrorism' Breeds More Terror&#133;and It's Unamerican Too!" That's how the call to action for the April 20 national demonstration against George W. Bush's war begins.</P>
<P>It's certainly true that the U.S. war "breeds more terror"--most of all, by unloosing the Pentagon war machine, the world's most deadly tool of terror. But is it "unamerican too"?</P>
<P>This argument is not uncommon among antiwar activists--that we should respond to the abuse dished out by flag-waving supporters of the war by insisting that we're the "real patriots."</P>
<P>Our side, say these activists, represents the true American spirit--the ideals of democracy and freedom won through struggle and enshrined in the Bill of Rights. As Michael Moore put it in a recent e-mail message to his mailing list: "When you send our kids to go fight and die on a foreign land so that you can finally build a pipeline for your oil backers across that country, THAT is un-American."</P>

