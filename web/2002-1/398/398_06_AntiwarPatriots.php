<P>But when you look at it, this argument doesn't hold up. First of all, as the Black revolutionary Malcolm X put it in the 1960s, "Violence is as American as apple pie." The foundation of the United States depended on the conquest of Native Americans, the capture of half of Mexico and the enslavement of millions of Africans.</P>
<P>And over the past century, America became a world superpower by carrying out wholesale violence and repression, from Latin America to Asia to Africa--in the name of enforcing U.S. political and economic domination.</P>
<P>In that way, it's very much "American" for Washington to send U.S. troops to die for an oil pipeline.</P>
<font face="Times New Roman, Times, serif" size="3"><P>Second, no matter how much opponents of war insist on their patriotism, they will always be denounced as un-American by the right. Martin Luther King learned this in the final years of his life when he took a public stand against the U.S. war in Vietnam.</P>
<P>In 1964, <I>Time</I> magazine named King its "Person of the Year." Three years later, the same magazine denounced his "Beyond Vietnam" antiwar speech at the Riverside Church in New York as "demagogic slander that sounded like a script for Radio Hanoi."</P>
<P>King and the civil rights movement had based their struggle against racism on the belief that they could pressure the U.S. to enforce rights supposedly guaranteed by the Constitution. But as the struggle developed--and as he faced the challenge of new political issues like the war--King began to change his views.</P>
<P>In his "Beyond Vietnam" speech, he didn't oppose the war as a separate injustice that could be put right if "America's ideals" prevailed. Instead, he connected the U.S. government's injustices abroad with injustices at home.</P>
<P>"We were taking the Black young men who had been crippled by our society and sending them 8,000 miles away to guarantee liberties in Southeast Asia which they had not found in southwest Georgia and East Harlem," King said. "So we have been repeatedly faced with the cruel irony of watching Negro and white boys on TV screens as they kill and die together for a nation that has been unable to seat them together in the same schools."</P>
<P>King's statement leads to a final point--one that King himself was still dealing with when he was killed one year later. The question is this: If we claim to be the "real patriots" or the "real Americans," what are we declaring our loyalty to?</P>
<P>It would be naive to believe that the U.S. is governed by the ideals referred to in the Declaration of Independence. The whole experience of U.S. history teaches that a small minority of people has the real say-so over the direction of society, because of their wealth or connections to wealth.</P>
<P>Unless challenged by struggle, this class of rulers controls what "America" represents--and therefore what it means to be patriotic.</P>
<P>Our loyalty shouldn't be to some imagined idea of what the U.S. <I>should</I> represent, but to the struggle to change the system and create a better world. As the great socialist leader Eugene Debs put it--in a speech that got him sent to prison for treason because he opposed the First World War: "Do not worry over the charge of treason to your masters, but be concerned about the treason that involves yourselves. Be true to yourself, and you cannot be a traitor to any good cause on earth."</P>
<P>Why is this discussion important? <I>Socialist Worker</I> supports the April 20 demonstration in Washington, despite the way that its call to action begins. We hope that our readers will help to organize the largest possible turnout, because this will show--in action, not in words--the growing opposition to Bush's war.</P>
<P>But in the process of organizing, activists need to discuss questions like these--and debate how best to explain what we stand for. It will be an important step forward for the antiwar movement to recognize that we aren't patriots--and that we're proud that our loyalties lie elsewhere.</P>

