<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Who are the real nuclear madmen?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 15, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IMAGINE IF George W. Bush obtained a secret tape recording of Iraqi President Saddam Hussein contemplating using a nuclear weapon. Bush would take to the airwaves to denounce the "evildoer" in Iraq, and the B-52s would be in Baghdad before Bush was finished speaking.</P>
<P>Recently, a secret tape recording did catch a president considering using a nuclear weapon. Only the president was Richard Nixon.</P>
<P>The tape, one of hundreds from Nixon's secret Oval Office taping system that the National Archives released last month, recorded Nixon suggesting the bomb to National Security Adviser Henry Kissinger in 1972.</P>
<P>Kissinger had presented Nixon with a list of escalation options, including destroying docks and power stations in North Vietnam. Nixon responded, "I'd rather use the nuclear bomb."</P>
<P>"That, I think, would just be too much," Kissinger replied.</P>
<P>"The nuclear bomb. Does that bother you?" Nixon responded. "I just want you to think big." Exactly what Nixon meant by "thinking big" we find out from a later conversation with special counsel Charles Colson: "We want to decimate that goddamned place."</P>
<P>Nixon succeeded in that plan. The U.S. war killed more than 3 million people in Southeast Asia and destabilized every country in the region. The U.S. dropped more ordnance on Vietnam than was dropped by all the armies of the Second World War.</P>
<P>Yet even if the U.S. decided against using nuclear weapons in Vietnam, it remains the only country ever to use nuclear weapons.</P>
<P>We should recall Nixon's words today as Bush threatens war against Iraq with the stated aim of preventing Iraq's development and deployment of nuclear, chemical and biological weapons.</P>
<P>If Bush calls Saddam a madman for attempting to acquire weapons of mass destruction, what does that make President Harry Truman, who actually ordered atomic attacks on Japan?</P>
<P>Leading commanders of the U.S. military, including Supreme Allied Commander in Europe Dwight Eisenhower, said Japan would have surrendered without the atomic bombings. Yet Truman was determined to send a message to USSR leader Stalin about who would call the shots in the postwar world.</P>
<P>In other words, Truman used the deaths of hundreds of thousands of people as a bargaining chip. Wouldn't that fall under Bush's definition of "evil"?</P>
<P>Not in Bush's world. In the nuclear pecking order, the most powerful countries--the ones that possess most of the nukes--lecture less powerful countries against acquiring them. Those that aren't their allies, that is.</P>
<P>Iraq, Iran and North Korea form an "axis of evil" in Bush's eyes, even though most experts believe that none of them has nuclear weapons. Yet, the U.S. considers Israel, the only nuclear power in the Middle East, part of the "coalition against terrorism." The U.S. has cozied up to both India and Pakistan, the newest members of the nuclear club, whose nukes are aimed at each other.</P>
<P>So the idea that the U.S. is leading "civilization" to stamp out "weapons of mass destruction" is bogus.</P>
<P>In the last two years, the U.S. has refused to ratify the updated Comprehensive Test Ban Treaty, scuttled an international treaty against chemical weapons, and withdrawn from the Anti-Ballistic Missile treaty. It is building a "missile defense" plan that aims to make other countries knuckle under to American nuclear blackmail.</P>
<P>Far from increasing the security of ordinary Americans, these schemes will fuel a new nuclear arms race. That's not a big concern to an administration whose key nuclear advisers once authored a report on nuclear war called "Victory Is Possible." That should make anyone question who the real nuclear madmen are.</P>
<P>The current White House crowd makes even Henry Kissinger seem reasonable.</P>

