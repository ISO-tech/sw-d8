<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Is the U.S. really out of recession?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 15, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"IT SEEMS quite clear now that our economy maybe never suffered a recession," Treasury Secretary Paul O'Neill declared last week. Tell that to the 8 million people out of work--and the millions more who've been hit with shorter hours or forced into part-time work. Even optimistic economists expect unemployment to increase in the months ahead.</P>
<P>Still, O'Neill has a point. Joblessness and social suffering have never been the official definition of a recession. Instead, the members of the National Bureau of Economic Research make an announcement--usually after the economy contracts for two consecutive quarters.</P>
<P>Last December, the bureau announced that the recession had begun in March 2001--and statistics showed a 1.3 percent decline in gross domestic product in the third quarter of that year. But new statistics claim that the U.S. economy started growing again in the last three months of the year, rising by 1.4 percent.</P>
<P>None of this means that the U.S. economy is out of the woods. Consider Japan. When the economic bubble of the 1980s burst, the government lowered interest rates to bail out banks loaded with bad debt and to avoid catastrophic corporate bankruptcies.</P>
<P>But the huge amounts of debt meant that banks were reluctant to make new investments without any certainty of making profits. Since the early 1990s, Japan has seen a series of weak recoveries, only to lapse back into recession months later.</P>
<P>Today, the U.S. economy faces many similar problems. Even if the recession is technically over--which is not certain--corporate debt remains at an astonishing six times the level of corporate profits, about $4.9 trillion. This debt will put a new damper on growth, since debt-laden companies will be reluctant to undertake risky new investments--just as they were in Japan a decade ago.</P>
<P>Indeed, a worldwide glut in industries, from steel to fiber-optic networks, has driven down profits--and triggered high-profile bankruptcies like Enron, LTV Steel and the telecommunications company Global Crossing. Capitalists will therefore pursue more "restructuring"--involving the closure of plants and offices and more layoffs for working people.</P>
<P>Nor will "the consumer" bail out the U.S. economy, as some pundits predict. This statistical category makes no distinction between social classes, masking the enormous differences in spending between them.</P>
<P>Consumer debt is at a record $7.5 trillion, and individual bankruptcies are at all-time highs, the result of workers trying to compensate for stagnant wages in the 1990s by going into debt. But the middle class has been hit, too--the stock market decline has wiped out as much as $400 billion in spending.</P>
<P>The U.S. economy remains vulnerable to other financial shocks. The current account deficit--the total amount owed for imports of goods and capital--remains at record highs. If foreign investors pull back, it could cause a stock market crash, a fall in the value of the dollar and inflation.</P>
<P>Nor will George W. Bush's tax cuts and military spending boost the economy back into a 1990s-style boom. Even if these measures spur growth somewhat, the money will be taken from spending on schools and health care and used for new bombers and missiles and tax breaks for billionaires.</P>
<P>Precise predictions about the economy are impossible. But there's a good chance that the U.S. economy dipped, is climbing, but will dip again--in what <I>New York Times</I> columnist Paul Krugman calls, appropriately enough, "the W scenario."</P>
<P>We need to make sure that this scenario also includes a challenge to a system that always puts profits before human needs--in booms as well as busts.</P>

