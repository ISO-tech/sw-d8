<P><font face="Arial, Helvetica, sans-serif" size="2"><b>SOUTH KOREA</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Power workers strike against privatization plan</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Scott Johnson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 15, 2002 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MORE THAN 5,000 workers at Korean Electric Power Corp. (KEPCO) have been on strike against privatization since February 25, in spite of threats of arrest and violence by the government. A top union official was arrested Monday, as management fired 49 of 52 union leaders.</P>
<P>The walkout initially included rail and gas workers, who settled after gaining concessions on working hours. The coordinated strike was part of a long-term fight for a 40-hour workweek and against the government's privatization drive.</P>
<P>On February 26, more than 100,000 workers from the Korean Confederation of Trade Unions held a four-hour general strike to support the power and rail workers. The general strike included workers at three of the country's four main auto companies--Hyundai, Sangyong and Kia--as well as workers in the steel and insurance industries.</P>
<P>President Kim Dae-jung has declared the power strike illegal because it is against a public utility. On March 5, KEPCO executives held a press conference to declare that they were breaking off negotiations and stepping up their crackdown on strikers.</P>
<P>The day before, some 10,000 police searched unsuccessfully for leaders of the union. The government has also threatened to shut down the power union's Web site, since it was being used to organize the walkout.</P>
<P>Most strikers are living at the country's universities, which police remain afraid to enter since the great battles of the pro-democracy movement of the late 1980s. Meanwhile, several dozen union leaders are staying at the Myongdong Cathedral in Seoul, a traditional haven for dissidents.</P>
<P>South Korean workers have been fighting neoliberal policies for years and have won important concessions with militant strike action, only to see them taken back when the strikes end.</P>
<P>This spring, unions plan to go on the offensive with a series of labor actions to disrupt the World Cup soccer tournament, which will be held in South Korea in June. Presidential elections are set for December, and the unions hope to embarrass Kim's ruling Millennium Democratic Party in the months beforehand.</P>

