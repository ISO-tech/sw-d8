<P>Under the cover of the "war on terror," the Bush gang has pushed ahead on a number of fronts, both internationally and domestically.</P>
<P>They got away with this in the immediate aftermath of September 11 by exploiting people's horror at the attacks. Any opposition to the White House, no matter what the issue, was branded as "forgetting the victims"--or worse, "aiding the terrorists," the slur used to denounce planned protests against the IMF and World Bank last September that were ultimately canceled.</P>
<P>But the last few months have seen the balance begin to shift. The deployment of U.S. troops and advisers to a growing number of countries--plus George W. Bush's insane "axis of evil" rhetoric, targeting enemies from Asia to the Middle East--has led many people, even some who supported the war, to question it today.</P>
<P>Antiwar activists who kept up the fight during the claims of U.S. "victory" in Afghanistan now have the opportunity to reach a wider audience as the war drive has less and less to do with September 11.</P>
<P>At the same time, Bush's tax cuts for the rich and a 30 percent increase in the military budget while schools crumble has led many to question the government's priorities.</P>
<P>And even as the media celebrate an apparent economic recovery, continued layoffs show that workers are being made to pay the price--while employers use Enron tactics to preserve their wealth and power.</P>
<P>This class bitterness is behind new sparks of resistance to Bush's domestic agenda--from student protests against budget cuts and tuition hikes in Massachusetts and Chicago, to the 1,500-strong demonstration against welfare "reform" in Washington, D.C., last week.</P>
<P>It will take time for these protests to take root and grow. And if they are to be effective, they will need to make the connection between opposing a war for U.S. imperial power abroad and Corporate America's war on workers at home.</P>
<P>That's why the series of demonstrations set for April 19-21 in Washington, D.C., is so important. The protests will bring together opponents of U.S. militarism alongside global justice activists demonstrating against IMF and World Bank meetings taking place that same weekend.</P>
<P>Activists from different movements have an opportunity to make the connections between our struggles concrete. Building the largest possible turnout for Washington can send a message that there's an opposition to the agenda of U.S. imperialism and corporate greed--and become a springboard for further resistance.</P>

