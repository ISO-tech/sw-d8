<p><font face="Arial, Helvetica, sans-serif" size="2">March 15, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>IF ANYONE doubted the real aims of the U.S. "war on terror," the disclosure of the Pentagon's nuclear hit list last week made matters horrifically clear. The fanatics who run the Bush White House are ready to unleash weaponry that could destroy the planet in their drive to dominate the globe militarily--as U.S. corporations dominate economically.</P>
<P>Yet the U.S. mainstream media mostly ignored the nuclear hit list last week--instead marking the six months since the September 11 attacks by retelling stories of the victims. But the increasing disconnection between September 11 and U.S. plans for the next stage of its "war on terror" wasn't lost on other countries, including U.S. allies.</P>
<P>And in elite U.S. publications, writers are openly admitting what Washington's war drive is all about--not September 11, but strengthening the power of U.S. imperialism. "The logic of neoimperialism is too compelling for the Bush administration to resist," one author wrote in the journal <I>Foreign Affairs.</I> "The question is not whether the United States will seek to fill the void created by the demise of the European empires but whether it will acknowledge that this is what it is doing."</P>
<P>Of course, U.S. imperialism is also using economic and political pressure to achieve Washington's goals--most notably in Argentina, the site of a massive rebellion last December against International Monetary Fund (IMF)-imposed free-market policies.</P>
<P>Such unrest won't do, say two top U.S. economists writing in the <I>Financial Times</I>. Argentina "must temporarily surrender its sovereignty on all financial issues" to Western politicians and bankers "for an extended period, say five years," they wrote last week.</P>

