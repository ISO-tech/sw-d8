<P><font face="Times New Roman, Times, serif" size="4">Alexander Cockburn on Bush's war:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"People have begun asking questions"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 15, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ALEXANDER COCKBURN is a columnist for the <I>Nation</I> magazine and coeditor of the left-wing newsletter <I>CounterPunch</I>. He talked to NICOLE COLSON about the course of Bush's "war against terrorism"--and the future of the antiwar movement.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<B><P>THE BUSH administration declared "victory" pretty quickly in Afghanistan, and a lot of pro-war liberals, like <I>Salon</I> editor David Talbot or <I>Nation</I> columnist Christopher Hitchens, say that we should admit we were wrong to oppose this war. What would you say?</P>
</B><P>GOD, THEY all say this. David Talbot. There's a guy called Michael Shuman, formerly the head of the Institute for Policy Studies, who just wrote a really stupid piece. Todd Gitlin, of course. One idiot after another.</P>
<P>Well, I'd say, "What victory?" The basic question comes down to this: If you're declaring victory in a war on terror, have you therefore defeated the likelihood or the prospect of terror?</P>
<P>They may have temporarily arrested plans, but have they materially decreased the risk of terror or the motives for terror? No, absolutely to the contrary. They've increased them.</P>
<B><P>WHAT DO you think will be the legacy of the attacks on civil liberties?</P>
</B><P>JUSTICE DEPARTMENTS and attorneys general always use emergencies and crises like September 11 to push through their agenda.</P>
<P>Basically, a lot of the PATRIOT Act was stuff that they couldn't get through in 1996, when Clinton signed the Anti-Terrorism and Effective Death Penalty legislation. A lot of it was part of the Justice Department's wish list then.</P>
<P>They're always ready to do it. So the civil liberties fight is an eternal fight.</P>
<B><P>WHAT HAS been the role of the mainstream media in this?</P>
</B><P>I THINK the mainstream press is getting into a phase where it has more reservations. You can see it on a number of levels.</P>
<P>I think one of the factors might have been the Enron affair, and I think also the economic stimulus bill. Also, the fact of the matter is that a lot of people thought the "axis of evil" stuff is insane.</P>
<P>And so, in that atmosphere, it became a little more permissible to ask questions like, "How many people were killed in the bombing?" Gradually, I think people have begun asking questions like, "Was this war such a good idea?"</P>
<B><P>WHAT DO you think is next on the agenda for the Bush administration?</P>
</B><P>I DON'T think they know. I think a lot of this is a bit like the discussion of the PATRIOT Act before Christmas.</P>
<P>They began with the PATRIOT Act, and crucially, in October, there wasn't much resistance, except from a few people--some of them on the conservative side actually, like Rep. Bob Barr (R-Ga.). And they thought, "We can get away with anything." So they proposed military tribunals, and they ran into a bit more resistance than they thought.</P>
<P>I think that when they put out this "axis of evil" nonsense about Iran, Korea and Iraq, some of it is just putting your finger in the wind to see what will happen. Then they can always say, "No! We're not going to attack North Korea. We're not going to attack Iran. But, yes, we are going to take out Iraq." And everyone says, "Well, okay, that's all right."</P>
<P>So I don't think they really know exactly what they're going to do. I think it's very much a matter of the political temperature--especially of how much resistance there is.</P>
<B><P>HOW DO you think the antiwar movement should react?</P>
</B><P>I THINK the antiwar movement was a little nervous and intimidated. There's been a lot of bullying and ranting by people like Hitchens and Talbot.</P>
<P>I think some people were a little nervous about what they should say and do--because they felt, obviously, that the September 11 attacks were terrorist attacks and were wrong.</P>
<P>But I think that the peace movement has to realize that it's on stronger ground than it might suppose.</P>
<P>What's actually happened in Afghanistan? How many al-Qaeda people were actually caught or killed? What happened to Osama bin Laden? Has the security and safety of people in Afghanistan been enhanced?</P>
<P>With all of these questions, you should look closely and not feel ashamed to ask them.</P>

