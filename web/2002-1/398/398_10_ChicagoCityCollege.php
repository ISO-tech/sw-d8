<P><font face="Times New Roman, Times, serif" size="5"><b>Chicago City Colleges</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Kyle Preckwinkle</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 15, 2002 | Page 10</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--About 500 students, faculty and staff protested the decision of the Chicago City Colleges Board of Trustees to gut counseling services.</P>
<P>Ten years ago, the number of counselors was already low at 60, but today there are only 19. The ratio of counselors to students is 1 to 3,833--compared to a ratio of 1 to 300 at most private schools.</P>
<P>So when the Board of Trustees produced a study that found counseling services "inadequate," the logical thing would have been to hire more counselors. Instead, the board proposed the elimination of counseling as we know it.</P>
<P>This attack is part of Mayor Richard Daley's long-standing war of attrition on city unions. But students and faculty are not taking this sitting down.</P>
<P>The trustees may have made their "decision," but this fight is far from over. There will be a walkout March 14 as well as a protest at City Hall.</P>
<P>We have to stop the assault on working-class and minority students, and we'll keep fighting until we get the counselors their jobs back.</P>

