<p><a name="laborers"></a><font face="Times New Roman, Times, serif" size="4"><b>Chicago day laborers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By H&eacute;ctor Reyes</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--Day laborers and their supporters concluded a six-day hunger strike after receiving guarantees that a city ordinance protecting day laborers' rights would be voted on at the next city council meeting.</P>
<P>The Latino Union of Chicago organized the action with the support of the Illinois Human Rights Collaborative and the International Coalition of Mexicans Abroad. The ordinance had been submitted last June by Alderman Billy Ocasio, but the city council hasn't taken any action on it.</P>
<P>Among the most significant issues addressed by the ordinance are the timely payment of workers' wages, the regulation of employer responsibility for medical expenses resulting from workplace accidents, and a ban on non-payroll tax deductions that many day labor agencies use to rip off workers through fees.</P>
<P>Activist Jos&eacute; Landaverde said that they were compelled to go on a hunger strike. "The ordinance was practically hidden because forces in the city council had taken an attitude which tried to keep the ordinance from being brought up for discussion," he said.</P>
<P>The city council next meets March 27, and the Latino Union is planning actions for the week before to keep up the pressure and create public awareness of the injustices faced by day laborers.</P>
<I><P>For more information about the struggle for day laborers' rights, contact the Illinois Human Rights Collaborative at 847-298-4843 or e-mail thelatinounion@aol.com.</P></I>

