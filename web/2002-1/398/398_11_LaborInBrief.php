<p><a name="Taco"></a><font face="Times New Roman, Times, serif" size="4"><b>Boycott Taco Bell</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Donald Taylor II and Jon Wexler</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IN MARCH, the Taco Bell Truth Tour made several stops in its coast-to-coast caravan to rally support for the boycott organized by Florida tomato pickers.</P>
<P>In Chicago, 150 people marched on March 3 in the bitter cold alongside workers from the Coalition of Immokalee Workers (CIW), the community-based organization that represents workers who pick tomatoes for Six Ls, Inc.</P>
<P>Six Ls is a chief tomato supplier to Taco Bell. Workers are asking for better wages, better housing and respect.</P>
<P>Tomato growers have paid workers the same amount--40 to 45 cents per bucket--for more than 20 years. A worker needs to pick about two tons of tomatoes to make just $50 a day.</P>
<P>CIW members are asking that Taco Bell pay 1 cent more per pound for its tomatoes. If growers passed the penny on to workers, it would double their wages.</P>
<P>Workers hope their national boycott will pressure Taco Bell into sitting down to talk. Workers also have no health insurance, sick leave, holidays, and no right to organize, and are forced to work long hours in the field with few breaks.</P>
<P>And the companies involved are far from broke. Taco Bell earned $5 billion in 1999, and its parent corporation Tricon, which also owns Pizza Hut and KFC, made $22 billion last year.</P>
<P>The Truth Tour stopped in Doraville, an Atlanta suburb, on March 1. Chanting "Yo no quiero Taco Bell," 50 CIW members and supporters marched from United Auto Workers Local 10 union hall to Taco Bell.</P>
<P>Demand an end to Taco Bell and Six Ls corporate greed and racism. Demand they pay a penny more for justice. Boycott the Bell!</P>
