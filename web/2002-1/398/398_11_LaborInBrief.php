<P><font face="Times New Roman, Times, serif" size="4"><b>New Era Cap</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Brian Erway</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>BUFFALO, N.Y.--A 150-strong crowd of strikers from the New Era Cap Co. and their supporters marched on the University of Buffalo (UB) campus March 6.</P>
<P>Workers from the Derby, N.Y., plant produce hats used by Major League Baseball as well as sports apparel for dozens of colleges, including UB. They've been on strike since July 16, after the 300 workers voted to join Communications Workers of America Local 14177.</P>
<P>"The management was heavy-handed and acted with impunity," Local Secretary Jason Kozlowski told <I>Socialist Worker</I>. "They didn't think we could sustain a strike against them. But now we're back in negotiations with four bargaining dates set for this month. We've put pressure on the company to seriously deal with a real unionized work force."</P>
<P>Although about 70 workers are crossing picket lines, most strikers remain solid--despite the fact that their unemployment benefits are running out. "Absolutely, our strike is having an impact on the company," said Kozlowski. "They'll never admit it, but they are 14 to 15 weeks behind schedule on orders."</P>
<P>New Era strikers have been receiving support from groups like Jobs with Justice and United Students Against Sweatshops, whose Worker Rights Consortium investigated the factory and found that the injury rate there is five times higher than the national industry average.</P>
<P>Now's the time for supporters to step up and give a boost to the strikers as they push for a settlement.</P>
<I><P>Donations and messages of support can be sent to CWA 14177, 7455 Erie Road, Derby, NY 14047.</P></I>

