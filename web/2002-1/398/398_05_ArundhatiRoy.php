<P><font face="Arial, Helvetica, sans-serif" size="2"><b>INDIA</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>A day in jail for the crime of free speech</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 15, 2002 | Page 5</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WHAT'S THE price of criticizing the Indian government for destroying the environment and attacking the poor? A day in jail.</P>
<P>Award-winning author and global justice activist Arundhati Roy spent 24 hours in jail and paid a small fine after the Supreme Court of India found her guilty of "contempt" by "scandalizing it and lowering its dignity through her statements."</P>
<P>Roy's crime? Free speech.</P>
<P>In October 2000, Roy joined a peaceful protest outside the Supreme Court as it considered approval of the Narmada Dam, the country's largest hydroelectric project. If the dam is built, it will cause an environmental disaster and a humanitarian crisis--forcing many of the 400,000 mostly poor people who live in the region to relocate.</P>
<P>The day after the October demonstration, a group of pro-dam lawyers filed a petition full of outrageous lies, claiming that Roy was drunk, shouted abusive slogans and even tried to murder them. So the Supreme Court began investigating Roy and ordered her to appear in court.</P>
<P>In response, Roy defended her right to free speech. She wrote that the Court's investigation showed "a disquieting inclination on the part of the court to silence criticism and muzzle dissent, to harass and intimidate those who disagree with it." This was apparently too much of an insult for the Court to take, so Roy was charged with contempt.</P>
<P>But as she prepared to serve the 24-hour sentence, Roy struck a defiant tone. "Anybody who thinks that the punishment for my supposed crime was a symbolic one day in prison and a fine of 2,000 rupees is wrong," she told reporters. "The punishment began over a year ago, when a notice was issued to me to appear personally in court over a ludicrous charge which the Supreme Court itself should never have entertained&#133;The message is clear: Any citizen who dares to criticize the court does so at his or her peril."</P>

