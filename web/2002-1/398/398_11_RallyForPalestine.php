<P><font face="Times New Roman, Times, serif" size="5"><b>Justice for Palestinians</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Wengraf</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 15, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--About 150 protesters demonstrated against Jerusalem Mayor Ehud Olmert and former New York City Mayor Rudy Giuliani outside Cooper Union March 7. Olmert and Giuliani gave speeches about Israel's supposed "quest for victory over the forces that seek to undermine our democratic values."</P>
<P>Of course, the Israeli government's "quest for victory" is nothing more than military terror used to maintain their decades-long occupation of Palestinian land. Olmert is responsible for expanding the Israeli occupation of East Jerusalem and the West Bank and pushing Arabs from their homes.</P>
<P>He is an open supporter of Israeli policies of "purification" of all Palestinians from Israeli land and has consistently called for "all-out war" against Palestinians. "We believe in the future and strength of the eternal and undivided Jerusalem and therefore our reaction is to answer the terrorists with more building, more roads," Olmert declared last month.</P>
<P>The forum--sponsored by the Caravan for Democracy, a project of the Jewish National Fund--brought out a handful of right-wingers who spewed racist slogans at the demonstrators.</P>
<P>But they were far outnumbered by pro-Palestinian activists from organizations such as Jews Against the Occupation, Al-Awda Right of Return Coalition, Palestinian student groups from Hunter College and Columbia University and the Direct Action Network. Chanting "Arabs and Jews unite, end the occupation" and "Free, free Palestine," protesters kept up a loud protest for several hours.</P>
<P>"This kind of showing is very important right now, when Israel is escalating the killing," said Ayman El-Sawa of Al-Awda. "But we aren't afraid because we are right and they are wrong. Look how few of them there are here today--they have nothing to say. And look at the soldiers who won't fight. So we're going to keep on fighting."</P>

