<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ZIMBABWE</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Government on a rampage of violence</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Mugabe tries to steal an election</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 15, 2002 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ZIMBABWE'S PRESIDENTIAL election was still in doubt amid fraud, violence and legal challenges as <I>Socialist Worker</I> went to press.</P>
<P>President Robert Mugabe's ZANU-PF party tried to guarantee victory by cutting the number of polling stations in urban areas by half and continuing a campaign of arrests and assaults against leaders and activists in the opposition Movement for Democratic Change (MDC). But the big turnout and long lines in the capital city of Harare compelled a court to order the extension of voting nationwide for a third day, to March 11.</P>
<P>Mugabe defied the order in the countryside. But the big turnout in the cities forced the government to reopen polls in Harare, where support for Mugabe's rival, MDC leader Morgan Tsvangirai, is overwhelming.</P>
<P>"Many people had to wait six or seven hours to vote on Saturday," said Tafadzwa Choto of the International Socialist Organization-Zimbabwe (ISO-Z). During the voting, ISO-Z activists toured the constituency of Munyaradze Gwisai, a member who was elected to parliament as part of the MDC two years ago, and urged voters to use their rights.</P>
<P>The government was expected to inflate Mugabe's vote totals in rural areas, where most of his support is based, in order to claim victory. Moreover, the chief of the military and security forces declared months ago that he wouldn't accept any outcome other than a Mugabe victory.</P>
<P>If there is an attempt to steal the election, the Zimbabwe Confederation of Free Trade Unions (ZCTU)--formerly led by Tsvangirai--has discussed calling a mass stayaway. "The question is whether that action can be sustained, because it is likely that Mugabe will move to crush it," Choto told <I>Socialist Worker</I>. "The next few days will be critical."</P>
<P>There's a chance that Mugabe could step down if the vote for Tsvangirai is too large to fake. But security forces could respond by carrying out a coup to keep ZANU-PF in power.</P>
<P>Already, authorities have charged Tsvangirai with treason for allegedly plotting the assassination of Mugabe. Scores of leading oppositionists also face criminal charges, including Gwisai, and there were reports of the arrest of a top MDC official at press time.</P>
<P>Other opponents of Mugabe have been kidnapped by ZANU-PF gangs and taken to torture camps--including Ephraim Tapa, president of the Civil Service Association of Zimbabwe, a ZCTU affiliate, and his wife, teacher Faith Mukwakwa.</P>
<P>These are only the latest examples of Mugabe's growing authoritarianism since he imposed a free-market "structural adjustment" program a decade ago at the urging of the International Monetary Fund (IMF).</P>
<P>Over the past two years, he has tried to rebuild support by organizing the rural poor to occupy huge farms still owned by whites, who stole the land during the colonial era. This led to a clash with the IMF and Western governments, which is why Mugabe today talks about defying the West.</P>
<P>But supporting the occupations was a cynical stunt--coming from a man who postponed land reforms for two decades since rising to power after leading the country's liberation struggle.</P>
<P>In any case, widespread shortages of food have cut into Mugabe's support in the countryside. The urban working class, which suffers from catastrophic rates of unemployment, supports the MDC and Tsvangirai.</P>
<P>But while ousting Mugabe would be a step forward for working people, Tsvangirai won't address their needs. He and other MDC leaders endorse the IMF's free-market program--and they have increasingly looked to Britain and the U.S. to pressure Mugabe with sanctions rather than organizing mass resistance.</P>
<P>That's why it's crucial that the struggle to defend unions and political rights continue no matter what the outcome of the election crisis.</P>

