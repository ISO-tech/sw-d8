<P><font face="Times New Roman, Times, serif" size="5"><b>What's at stake in UTLA election</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Michal Myers, UTLA Chapter Chair</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 15, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LOS ANGELES--Teachers, nurses and counselors of United Teachers Los Angeles (UTLA) voted for a new president in a hotly contested four-way election last week, setting the stage for a runoff this month between John Perez and Becki Robinson.</P>
<P>The two candidates--along with another, Mike Cherry--were vice presidents under outgoing UTLA President Day Higuchi but split in different directions after bitter disputes over the last contract.</P>
<P>Cherry, as the union's point person on mobilizations, involved thousands in picketing, leafleting and rallies in our contract struggle two years ago--including a march of 4,000 during the 2000 Democratic National Convention. But as November approached, Higuchi, Perez and Robinson called off mobilizations.</P>
<P>Cherry called for a strike mobilization, but in early 2001, a settlement was reached. We got a three-year contract with an average onetime raise of 10 percent--but also concessions on workplace rights, an exaggerated two-tier salary system and a contract reopener for wages and benefits in the second and third year.</P>
<P>So far, the "reopener" has amounted to a wage freeze, and we've been threatened with new co-pays for health insurance.</P>
<P>Perez--Higuchi's handpicked successor--defended the contract. He spent $25,000 on his campaign and was backed by bigwigs from our parent union, the National Education Association (NEA).</P>
<P>Robinson, who paints herself as a fighter, said at the time that the contract was "the best we could do without hurting kids."</P>
<P>By contrast, Cherry admitted that the last contract had problems--and blamed them on a failure to mobilize and take a hard line in negotiations. His campaign motto in this election&#150;"It's time to take the gloves off!"--represented the fighting spirit we needed to go forward.</P>
<P>What happened? Unfortunately, the two most conservative candidates, Perez and Robinson, came out ahead. One reason is that only 23 percent of the 43,000 members voted.</P>
<P>The other reason has to do with a fourth candidate--Warren Fletcher--who ran as an "outsider," directing much of his fire against Cherry. This was a mistake.</P>
<P>Cherry had argued for mobilizations, made sure that the UTLA contributed to the defense fund for South Carolina dockworkers facing jail, and voted for the union to oppose the U.S. war in Afghanistan.</P>
<P>At my school, he told us he planned to "use the 's' word and mean it"--in other words, organize for a strike--and he said he would "rely on the rank-and-file members if other do-nothing officials got in the way of confronting the district."</P>
<P>This wasn't the time to divide the left's vote in our union. We face a $56 million budget cut in LA, increasing overcrowding, pressure for teachers to be "accountable" through standardized tests and a school board openly discussing 1,000 layoffs.</P>
<P>Even with the low voter turnout, Cherry and Fletcher got 40 percent of the votes combined! A unified left would have won the election.</P>
<P>Whoever wins this runoff, union members can't let the state and district push the cost of the recession on teachers and schools. It's time to hit the streets again and fight back for ourselves, the children we teach and all working-class people.</P>
<P>Those who supported Cherry and Fletcher must unite and look to the rank and file for strength.</P>

