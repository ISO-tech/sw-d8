<P><font face="Times New Roman, Times, serif" size="5"><b>Students fight cuts at UMass</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Adam Meringolo</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 15, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHANTING "THEY say cut back, we say fight back," more than 1,500 students, faculty and staff rallied outside the student union at the University of Massachusetts-Amherst March 7 to protest budget cuts and a tuition hike.</P>
<P>Last December, administrators announced a budget that slashed spending throughout the UMass system--and an emergency tuition increase of $495 for the next semester.</P>
<P>And there's worse to come. Fee increases in excess of $1,200 are due to take effect next school year, and administrators have more cuts in store.</P>
<P>Acting Gov. Jane Swift is trying to make up for a $2 billion budget shortfall by attacking the state university system. At UMass-Amherst, some 300 workers are being threatened with layoffs. The entire non-police security force--which gained popularity three years ago during a wave of sexual assaults on campus--is on the chopping block. Janitors and maintenance workers also face cutbacks.</P>
<P>Meanwhile, the tuition hikes will make it harder than ever for working-class students, especially at the smaller community colleges in the state system. At Berkshire Community College, for example, 10 percent of students say that they can't return for the spring semester. This school and others are considering closing their open-door policy because resources are so scarce.</P>
<P>At UMass-Amherst, one major attack on faculty and students with children is the closure of university child care. So much for interim Chancellor Marcellette Williams' claim that "we're a family."</P>
<P>In fact, Williams is one person who won't have to worry about the cuts. When she took over for the old chancellor last July, she got a 38 percent pay increase to $220,000 a year. And UMass President William Bulger saw his already bloated salary climb 10 percent to $309,000. Yet these are the very people who say that we have no choice but to cut back.</P>
<P>At the rally, speakers from the Graduate Employee Organization and the newly unionized Resident Assistants talked about the importance of students and staff making their voices heard.</P>
<P>The big turnout at the rally is a start. But we'll need to step up our mobilization to stop these attacks.</P>

