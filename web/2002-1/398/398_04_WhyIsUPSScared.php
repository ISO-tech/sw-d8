<P><font face="Times New Roman, Times, serif" size="5"><b>What is UPS scared about?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 15, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>As Teamsters and UPS begin contract talks, UPS is intent on keeping workers in the dark. That was the case this week when managers at UPS in Williston, Vt., tried to stop the sale of <I>Socialist Worker</I> outside company gates.</P>
<P>When they realized they had no legal grounds to stop the sale, the bosses hovered at the entrance in an obvious attempt to intimidate any worker who stopped to buy a paper.</P>
<P>What do these bosses have to be afraid of? Plenty. After all, rank and filers carried UPS Teamsters to labor's biggest victory of the 1990s.</P>
<P>Just a few weeks ago, management launched a $45 million ad campaign--"What Can Brown Do For You?"</P>
<P>What are the bosses at UPS in Williston afraid of? How about the fact that such a lame and pricey campaign is no match for the slogan of the 1997 strike "Part-Time America Won't Work."</P>
<P>Now there's a slogan that still rings true.</P>
<B><P>Nancy Welch,</B> AFT/AAUP, Burlington, Vt.</P>

