<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Texas oil boys in the White House</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 15, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH and Dick Cheney swear that they never did any favors for bankrupt energy giant Enron. But you'd never know it to judge from the White House energy plan that the Senate is debating this week.</P>
<P>The administration's proposals are full of giveaways to corporate polluters that the likes of Enron have been after for years. The best known is probably the scheme to open the Arctic National Wildlife Refuge to oil and gas exploration--a brazen gift to Big Oil with no other rational justification.</P>
<P>The drilling proposal will probably be defeated in the Senate. But there's a lot more in the Bush energy plan that will get by because Democrats and Republicans agree on them. For example, blocking higher fuel-efficiency standards for gas-guzzling sports utility vehicles, something that U.S. automakers could do easily, but only by spending a little money that would otherwise line their pockets.</P>
<P>Last month, Bush announced his "alternative" to the 1997 Kyoto Protocols, a global environmental treaty designed to reduce emissions of the greenhouse gases that are causing global warming. The Kyoto treaty wasn't even particularly tough. It allowed rich countries, for example, to purchase "pollution credits" from developing nations to continue polluting.</P>
<P>But even that was too much for Bush. His alternative, absurdly misnamed "Clear Skies," would allow corporations to peg their greenhouse gas emissions to the growth of the national economy.</P>
<P>This means that pollution would actually increase significantly. "This is the most anti-environmental act by an American president in modern history," said Alden Meyer of the Union of Concerned Scientists.</P>
<P>But that's no surprise, considering the source. For his entire political career, Bush has had nothing but contempt for environmental standards, lining up with big-business polluters at every turn.</P>
<P>And the human cost of the relaxed pollution restrictions that Bush wants? According to Eric Schaeffer--the former head of regulatory enforcement at the Environmental Protection Agency who recently resigned in disgust--the annual toll will be tens of thousands of premature deaths, thousands of cases of chronic bronchitis, and untold damage to the environment.</P>
<P>Bush and his cronies are putting our lives and the very future of the world in danger--in the name of profits.</P>

