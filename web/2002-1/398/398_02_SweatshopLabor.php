<P><font face="Times New Roman, Times, serif" size="5"><b>Nike and Adidas profit off sweatshop labor</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 15, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>INDONESIAN WORKERS who make shoes for multinational giants Nike and Adidas continue to face sweatshop conditions, according to a new report.</P>
<P>Investigators for the Australia-based Oxfam Community Aid Abroad found that conditions in shoe factories improved somewhat since the anti-sweatshop movement focused attention on the two multinationals' operations. But "the measures taken fall well short of ensuring that workers are able to live with dignity," the report concludes.</P>
<P>Full-time wages in the factories are approximately $56 a month--about $2 a day--which leaves workers in extreme poverty. Oxfam researchers estimate that half of all workers with children are forced to send them to live with relatives.</P>
<P>The study also found that shoe factory managers regularly target unionists. Last year, one worker, Ngadinah Binti Abu Mawardi, was arrested and imprisoned for a month for organizing a strike at the PT Panarub factory that makes Adidas shoes.</P>
<P>Both companies claim to monitor conditions at factories that produce shoes for them. But this study proves that Nike and Adidas are still getting rich off sweatshop labor.</P>

