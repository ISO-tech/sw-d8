<P><font face="Times New Roman, Times, serif" size="5"><b>The world's leading perpetrator of terror</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Keeanga-Yamahtta Taylor</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 15, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>BOOKS:</B> Noam Chomsky, <B><I>9-11.</B></I> Seven Stories Press, 2001, 126 pages, $8.95.</P></font>
<font face="Times New Roman, Times, serif" size="3"><I><P>9-11</I> IS composed of a series of interviews that Noam Chomsky did in the month after the September 11 attacks. The U.S. war in Afghanistan hadn't yet begun, but the trajectory of events clearly pointed toward a massive military campaign there.</P>
<P>Chomsky explicitly denounces any U.S. war effort based on two points. One, it will "visit a terrible cost on wholly innocent victims, many of them Afghan victims of the Taliban." And two, the U.S. itself is the main perpetrator of state-sponsored terrorism across the globe.</P>
<P>Chomsky backs this up with several examples of U.S. aggression, as well as examples of its willingness to ignore the atrocities of its allies.</P>
<P>The miserable "coalition" the U.S. cobbled together for the so-called war on terror is a perfect example. As Chomsky points out, U.S. allies "are delighted to see an international system develop sponsored by the U.S. which will authorize them to carry out their own atrocities."</P>
<P>Chomsky rightly calls the genocidal sanctions against Iraq--which have killed half a million children--an act of terrorism. And he describes the 1998 bombing of a Sudanese pharmaceutical plant, which the U.S. claimed had links to terrorism. Before it was destroyed, the plant produced 50 percent of Sudan's medicine.</P>
<P>The press skewered Chomsky for comparing the Sudan plant bombing to the September 11 attacks. But the point he made still stands: "One can scarcely try to estimate the toll of the Sudan bombing, even apart from the probable tens of thousands of immediate Sudanese victims. The complete toll is attributable to the single act of terror."</P>
<P>Chomsky points out that, in 1986, the U.S. was in fact condemned by the World Court for "unlawful use of force (international terrorism) and then vetoed a Security Council resolution calling on all states to adhere to international law."</P>
<P>Not only did the U.S. ignore the ruling but immediately escalated its illegal war in Nicaragua, essentially thumbing its nose at the World Court and international law.</P>
<P>Chomsky is at his best when he's uncovering the crimes of the U.S. around the world. That's why <I>9-11</I> becomes somewhat confusing when Chomsky advocates that those responsible for the September 11 attacks be "brought to justice," without addressing how and by whom.</P>
<P>He suggests that events after the Oklahoma City bombing point to an appropriate response. "When it was discovered to be a domestically devised attack, by someone with militia connections, there was no call to obliterate Montana and Idaho&#133;Rather there was a search for the perpetrator, who was found, brought to court, and sentenced&#133;That is the course we follow if we have any concern for genuine justice and hope to reduce the likelihood of further atrocities."</P>
<P>But is the U.S.--which is responsible for so many atrocities, as Chomsky illustrates so well throughout this book--ever capable of delivering "genuine justice"?</P>
<P>A more effective strategy is for activists to point out the hypocrisy of U.S. intervention around the world before and since September 11, and, in Chomsky's words, "devote themselves with even more energy to the just causes to which they have already been committed."</P>
<P>Chomsky's <I>9-11</I> provides many of the tools to do just that.</P>

