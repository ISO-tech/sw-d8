<P><font face="Times New Roman, Times, serif" size="4">White House asks the Pentagon to draw up a...</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Nuclear hit list</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 15, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH gang is thinking about the unthinkable. In a secret policy review finished earlier this year, the White House ordered the Pentagon to draw up plans to use nuclear weapons against at least seven countries--Russia, China, Iraq, Iran, North Korea, Libya and Syria.</P>
<P>Events that would trigger U.S. nuclear strikes are terrifyingly close to happening right now--for example, an escalation of the Middle East conflict into a war between Israel and Arab countries, or virtually any war involving Iraq against neighboring Kuwait, Saudi Arabia or Turkey.</P>
<P>The White House's doomsday strategy even has a wildcard--any "surprising military development" could be a green light for pushing the button. According to the Pentagon document leaked to the <I>Los Angeles Times</I> last week, just about anything--including another terrorist attack on the scale of September 11--could qualify.</P>
<P>Revelations of the administration's so-called Nuclear Posture Review sparked outrage around the world. "The lunatics have taken over the White House," said one British lawmaker. But that's been clear for a while.</P>
<P>The Bush gang has made no secret of its willingness to consider using nukes. The review was authored by a group of strategists--prot&eacute;g&eacute;s of Defense Secretary Donald Rumsfeld--who have publicly supported the development of a new generation of "mini-nukes."</P>
<P>They claim that the Pentagon needs an arsenal of low-yield warheads that could be used against specific targets--to wipe out "rogue regimes" while keeping "collateral damage" to a minimum.</P>
<P>Rumsfeld, too, openly backs this madness. In a speech last year, he dismissed the principle of deterrence--the idea that countries are deterred from attacking the U.S. out of fear of being destroyed by nukes. Iraq, Rumsfeld argued, "knows a U.S. president would not drop a 100-kiloton bomb on Baghdad."</P>
<P>The solution? Build mini-nukes that the U.S. would actually consider using.</P>
<P>The Pentagon document is packed with insane proposals like these--everything from arming cruise missiles with nuclear warheads to a recommendation to train Special Ops soldiers to call in mini-nuke strikes.</P>
<P>On the first stop of his eight-country mission to build support for a U.S.-led assault on Iraq, Dick Cheney told reporters in Britain that the nuclear review was nothing to worry about. But the hypocrisy of the Bush gang and the whole U.S. government couldn't be more obvious.</P>
<P>Cheney will demand support for U.S. action against Iraq on the grounds that Saddam Hussein's government might be developing "weapons of mass destruction." Yet back at home, the White House is preparing to build its own weapons of total annihilation.</P>
<P>Washington's nuke-happy politicians and generals are the real madmen.</P>

