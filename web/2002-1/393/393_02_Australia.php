<P><font face="Times New Roman, Times, serif" size="4">Victims of government scapegoating</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Australia's war on refugees</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 8, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>POLITICIANS LIKE Australia's Prime Minister John Howard love to celebrate the U.S. government's war on Afghanistan. Afghans are at last free from the tyranny of Taliban rule, they say--and at the cost of only a few thousand people killed by U.S. bombs.</P>
<P>Howard and his pals are second to none in wanting to help Afghans. As long as they stay in Afghanistan.</P>
<P>When thousands of Afghan refugees fleeing Taliban repression and the U.S. war landed on Australia's shores, Howard had them rounded up and thrown in a prison. Or, more accurately, "a concentration camp," according to a representative of Australian religious groups.</P>
<P>Howard's government is enforcing mandatory detention of all men, women and children who enter the country without valid visas.</P>
<P>Woomera--one of six refugee prisons around Australia--is a former missile-testing base located on a hot, dusty plain, where temperatures regularly exceed 100 degrees. Conditions for the refugees are so bad that more than 200 staged a hunger strike for nearly two weeks. More than 40 detainees physically sewed their lips together in protest. A lawyer for the refugees says a number of young boys have threatened to commit suicide by jumping on the razor wire.</P>
<P>"We are detained in this hell for nothing," Leila, an asylum seeker at the Curtin Detention Center in Western Australia, told the country's national radio network last week. One doctor who visited the center told reporters that she had "never seen so many distressed, traumatized and disturbed children."</P>
<P>But don't expect sympathy from Howard. He says that none of the refugees are entitled to asylum--because Afghanistan has been "liberated."</P>
<P>Of course, hundreds of the refugees are ethnic Hazaras who fear persecution under the new Afghan government. But Howard doesn't care. He's so determined to make an example of the refugees that he's repeatedly turned down offers from charities to provide free housing and food for the asylum seekers. "Mandatory detention is part of the process of sending a signal to the world that you cannot come to this country illegally," he declared.</P>
<P>Howard scoffed at the idea that refugees are being driven to desperation by the inhumane conditions in the camps--and even suggested that parents were forcing their children to join in the hunger strike. "Children in the proper, positive care of their parents don't sew their lips together, do they?" he sneered.</P>
<P>Here's a man who isn't afraid to stand up for the real values of Bush's "war on terror."</P>

