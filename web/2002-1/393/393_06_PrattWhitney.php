<P><font face="Times New Roman, Times, serif" size="5"><b>"I'm not going to root for bullies"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 8, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>AMONG THE protesters in New York was a worker from the jet engine maker Pratt and Whitney, where members of the International Association of Machinists won a new contract after a controversial strike in December. He talked to <I>Socialist Worker</I> about that struggle--and why he came to New York.</P>
<P>WHAT DID the strike achieve?</P>
</B><P>THOUGH WE didn't get everything, it's probably the best contract in the aerospace industry. It doesn't completely stop layoffs--which is difficult to get, especially given the state of the economy.</P>
<P>The company created 5,000 hard-asses--people are really anti-company and pro-union.</P>
<P>Overall, our membership is pretty conservative. A lot of people voted for Bush, and it's a mostly white local. But there was no question about this strike, even though it came during this tough period [during the war on Afghanistan]. People made the decision to stop military production, even though they weren't necessarily against the war.</P>
<P>The company, of course, attacked us for being unpatriotic, and there were a few hints of that in the media.</P>
<B><P>HOW DO you see the global justice movement connecting with people you work with?</P>
</B><P>PEOPLE TALK about it, but more before 9-11. I think it's coming back now.</P>
<P>This flag-waving is going to be a problem for a while. But you have to take a position and hold it. 9-11 didn't change the world in every respect.</P>
<P>When people came after me and called me unpatriotic because they knew I opposed the war, I would tell them that you can wave the flag all you want, but I'm not going to root for bullies.</P>
<P>By waving that thing in my face, you're supporting domination of oppressed people by the same people that stomp on you--maybe a little bit less, but it's the same thing.</P>

