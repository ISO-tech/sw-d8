<P><font face="Times New Roman, Times, serif" size="5"><b>The Bush gang's fuel-cell con game</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 8, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH administration said in January that it was committing itself to a campaign to promote pollution-free cars that run on hydrogen-powered fuel cells. But the seemingly environment-friendly proposal was a cover--for abandoning a program to require fuel-efficient gas-powered cars in the here and now.</P>
<P>Under the fuel-cell idea, cars would run on electricity created from hydrogen and oxygen supplies--producing no emission other than water vapor. But the technology is at least a decade away from being practical.</P>
<P>At the same time, the Bush gang officially abandoned a multibillion-dollar program to require the Big Three automakers to produce more fuel-efficient cars.</P>
<P>But that's no surprise if you take a look at Bush's energy secretary: Spencer Abraham, a former senator from the state of General Motors, otherwise known as Michigan. Abraham's main accomplishment in Congress was to block a proposal to require higher fuel-efficiency standards for SUVs and light trucks.</P>
<P>But then again, an administration packed with Texas oil and gas boys doesn't need a lackey for the Big Three telling it to kill tighter standards on fuel efficiency.</P>

