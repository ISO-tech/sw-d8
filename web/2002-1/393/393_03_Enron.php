<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">The Enron gang and all its friends in Washington:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Crooked to the top</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 8, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>IF IT were all put in a movie, Rush Limbaugh would be hyperventilating about Hollywood's left-wing conspiracy mongering. But nearly every day brings an incredible new revelation about the collapse of energy giant Enron--and the greed, arrogance and dishonesty of its top executives and their buddies in Washington.</P>
<P>Consider a few choice examples:</P>
<P>--Last April, in the middle of the California power crisis, former Enron CEO Ken Lay met with Vice President Dick Cheney to argue against federal limits on skyrocketing electricity prices, and Cheney repeated Lay's words to the <I>Los Angeles Times</I> the next day.</P>
<P>--Lay lied continuously to employees about the company's health to keep them from unloading Enron stock in their retirement savings accounts--though he and other top executives had long before cashed out.</P>
<P>--One Enron executive who objected to the company's corrupt accounting schemes--a potentially key witness--was found dead in January, the victim of an apparent suicide.</P>
<P>--George W. Bush packed the federal agency that oversees the energy industry with candidates recommended by Lay in a memo sent to the White House last spring.</P>
<P>--The accounting firm Arthur Andersen kept destroying records of its Enron audits after it knew that government investigators were conducting an inquiry--and Enron itself didn't turn off the shredders until mid-January.</P>
<P>--For the first time in history, Congress is suing the White House--to obtain records from the task force chaired by Cheney that, with Enron's help, came up with the administration's energy policy.</P>
<P>The list goes on and on.</P>
<P>So much that's wrong with capitalism--the crimes of corporations that care only about profit, the corruption of a political system that serves the bosses, the disaster of deregulation and the free market--has been exposed by the Enron scandal. But you'd never know any of that to hear Bush's State of the Union address last week.</P>
<P>Bush was expected to try to distance himself from the scandal. And he did--he didn't use the word "Enron" even once. Bush was much too busy reading out the U.S. government's hit list for its new and expanded "war on terrorism." In effect, Bush said, "Look over there." And official Washington did just that.</P>
<P>In the Democrats' response, House Minority Leader Dick Gephardt (D-Mo.) mumbled briefly about campaign finance reform before returning to his party's running theme of blaming Bush for running up a deficit--incredibly, allowing Republicans to pose as the party that wants to use government resources to help those hurt by the recession.</P>
<P>Likewise, the mainstream media accepted the line of White House spin doctors that mentioning Enron was beneath the president. "To his credit," the "liberal" <I>Washington Post</I> editorialized, "President Bush delivered a wartime address, an honest and sober account of the long road that still lies ahead in the war against international terror."</P>
<P>Sober maybe--though "terrifying" might be a better description of Bush's warmongering. But honest?</P>
<P>This spineless attitude runs in exactly the opposite direction of opinion polls showing that ordinary people recognize the Enron scandal as a symbol of everything that's wrong with the U.S. political system--and the cravenly pro-business Bush administration in particular.</P>
<P>Bush may remain popular as a wartime leader. But growing numbers of people will come to see him as they did before September 11--a spoiled rich kid who stole the White House with the help of daddy's powerful pals.</P>
<P>If the Democrats won't take the offensive on Enron, it's because all of Washington is implicated in the scandal. The Democrats got less campaign cash from Enron. But they were no less willing to grease the wheels for the company's incredible rise--and inevitable collapse.</P>
<P>So whatever bluff and bluster you hear out of Washington, don't expect anything more than token reforms. Washington won't do anything that really affects corporate giants like Enron--because Washington is part of the same ruling class that runs the corporations.</P>
<P>We need to use the Enron scandal for something different--to expose an utterly unjust system. We need to take every opportunity to explain how Enron shows what's wrong with capitalism--and why we need a socialist alternative to this crooked system.</P>

