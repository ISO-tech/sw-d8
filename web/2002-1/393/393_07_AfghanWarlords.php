<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Warlords run the "new" Afghanistan</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 8, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>DURING HIS gloating State of the Union address, George W. Bush claimed the U.S. victory over Afghanistan "saved a people from starvation and freed a country from brutal oppression."</P>
<P>Thanks to U.S. bombs, Bush added, "Today, women are free and are part of Afghanistan's new government." He introduced Hamid Karzai as "the distinguished interim leader of a liberated Afghanistan" to thunderous applause.</P>
<P>Karzai was the toast of Washington last week. The fashion glitterati dubbed him the best-dressed diplomat-of-the-moment, while CNN heralded him as a "peaceful person." But news reports emerging from post-Taliban Afghanistan paint a far different picture of the U.S.-installed government.</P>
<P>The interim government has already made clear that harsh sentencing laws would remain in effect throughout Afghanistan, barely distinguishable from those of the Taliban. Judge Ahamat Ullha Zarif said that adulterers, both men and women, will continue to be stoned to death, "but we will use only small stones." Public executions and amputations will continue, he said, but with more civility. "For example, the Taliban used to hang the victim's body in public for four days," he said. "We will only hang the body for a short time, say 15 minutes," he said.</P>
<P>Karzai himself is no newcomer to Afghan politics. He served as deputy foreign minister in Afghanistan's lawless mujahideen government--which first required women to wear veils--and he also supported the Taliban when it first came to power in 1996.</P>
<P>Karzai's recent conversion to the cause of women's rights therefore rings hollow. Afghanistan's well-publicized new Women's Bureau, not surprisingly, has received neither an office nor funding.</P>
<P>Bush himself recently added to the suffering of Afghan women, using them as pawns in his domestic anti-abortion campaign. In January, he decided to hold back funds for the United Nations Population Fund on the false allegation, spread by anti-abortion extremists, that the group promotes abortion in China.</P>
<P>The Population Fund supplies sanitary supplies, clean underwear and emergency infant delivery kits to Afghan women refugees. These kits are desperately needed, since Afghan women have the highest maternal mortality rate in the world, and refugees are denied even basic obstetrics care.</P>
<P>The four months of U.S. bombing brought back the same feuding warlords who controlled Afghanistan during the 1992-96 mujahideen government before the Taliban came to power. Over half the seats in the interim government went to the Northern Alliance warlords, renowned for looting, raping and mass executions of civilians.</P>
<P>These battling factions are now back in control in most of Afghanistan, threatening to plunge the country once again into civil war and ethnic cleansing.</P>
<P>U.S. bombs have already killed thousands of Afghan civilians, and unexploded bombs littered across the country will continue killing for years to come. The bombing also prevented farmers from planting winter wheat and halted humanitarian food deliveries to the starving population.</P>
<P>Now some food delivery has resumed, but looting and banditry are preventing food from getting to the very people at the most risk for starvation this winter--those in outlying areas, including many refugee camps. "With different warlords controlling different roads, there are some areas where we just can't go," said World Food Program spokesperson Abby Spring.</P>
<P>At the Maslakh refugee camp, 30 miles west of Harat, for example, 100 refugees die each day from exposure and starvation. Villagers from Bonavash have resorted to making bread from grass to try to fend off starvation.</P>
<P>James Jennings, president of aid organization Conscience International, predicted that up to 1 million Afghans will starve to death before the summer. "Secretary Rumsfeld may think things are infinitely better in Afghanistan than before the war," he said, "but I doubt if most of the burqa-clad beggars I regularly see there would agree."</P>

