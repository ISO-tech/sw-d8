<P><font face="Times New Roman, Times, serif" size="5"><b>Protest Benjamin Netanyahu</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nell Crawford</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 8, 2002 | Page 10</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>EVANSTON, Ill.--Approximately 60 people turned out February 4 to protest the appearance of former Israeli Prime Minister Benjamin Netanyahu at Northwestern University. The protest turned into a victory rally when Netanyahu's appearance was canceled because of "increased security concerns," according to the university.</P>
<P>Northwestern University students and staff initiated the demonstration after hearing that Netanyahu was being brought to speak on the subject of "Defeating Terrorism." "I'm proud because I think our organizing the protest had an impact on their decision to cancel Netanyahu's appearance," student Lars Johnson told <I>Socialist Worker</I>.</P>
<P>The event was sponsored by the Medill School of Journalism's Crain Lecture Series and by conservative institutions like the Foundation for the Defense of Democracy--a post-September 11, right-wing foundation started by former New Jersey Senator Frank Lautenberg.</P>
<P>Students questioned the university's priorities when a large number of tickets were reserved for the members of foundations and institutions outside of Northwestern who donated money toward the event, making it nearly impossible for students to get tickets.</P>
<P>Much more alarming is that Netanyahu's visit fell at the end of Islam Awareness Week, during which a Palestinian student was taken in for questioning by local police as part of Bush's plan to round up 5,000 students of Middle Eastern descent. This is the third Northwestern student to be interviewed by the authorities.</P>
<P>Activists are planning to use the victory rally to kick off a campaign to demand that the university administration stop cooperating with the FBI and start protecting the rights of its international students.</P>

