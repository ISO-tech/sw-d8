<P><font face="Arial, Helvetica, sans-serif" size="2"><b>INSIDE THE SYSTEM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Cover-up at Justice</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 8, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ATTORNEY GENERAL John Ashcroft doesn't want to be seen in the same room with a semi-naked woman--even if she's just a statue.</P>
<P>Last week, Ashcroft's staff had draperies installed over two statues in the Justice Department's Great Hall--at a cost of $8,000. The statues, which date back to the 1930s, show a man and a women in toga-style garments.</P>
<P>Last year, after a press conference in the Great Hall, several newspapers ran photos of Ashcroft with the bared breast of the female statue featured prominently over his head. Ashcroft, a born-again fanatic, was so disturbed that he ordered the statues covered for "aesthetic purposes."</P>
<P>In the 1980s, the female statue's breast was also featured prominently in news photos--when former Attorney General Ed Meese released the Reagan administration's report on pornography.</P>
<P>--ABCNews.com, January 26, 2002</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>GM gives to the cause</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WHEN CAR giant General Motors paid $100,000 to use a snippet of a song in one of its commercials recently, the company had no idea that it would be funding the anticorporate struggle.</P>
<P>To GM, the song "Pass It Along," by the British band Chumbawamba, seemed to fit perfectly with its new "Pass It On" ad campaign. But GM ad execs apparently didn't check up on band members--who identify themselves as anarchists--or listen to the lyrics of the song, which are about why music should be free.</P>
<P>"The best things in life are free," the band sings in the chorus. But not for GM.</P>
<P>After much debate, the band accepted the $100,000 fee that GM was offering to use the song--and then promptly donated half to the independent news service Indymedia and half to the anti-corporate watchdog group CorpWatch.</P>
<P>"We plan on using some of the money to monitor GM's social and environmental impacts," said a spokesperson for CorpWatch.</P>
<P>--<I>Salon</I> magazine, January 30, 2002</P>

