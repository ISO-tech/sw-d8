<P><font face="Times New Roman, Times, serif" size="4">Bush's new scheme to chip away at our rights</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Anti-abortion fanatic in the White House</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 8, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IN HIS State of the Union address, George W. Bush piously declared that "America will always stand firm for the non-negotiable demands of human dignity"--including "respect for women." But in the two weeks surrounding the speech, Bush showed repeatedly that he has no respect for women--particularly when it comes to their right to control their own bodies.</P>
<P>On January 22, Bush spoke via telephone to an anti-choice rally in Washington, D.C., on the anniversary of the 1973 <I>Roe v. Wade</I> decision that gave women the legal right to abortion. Then last week, the administration went further--showing that ending abortion rights is on its agenda.</P>
<P>The latest attack came from Health and Human Services Secretary Tommy Thompson, who unveiled a new scheme that could potentially lead to the outlawing of all abortions. Under the plan, embryos and developing fetuses would qualify under the Children's Health Insurance Program (CHIP)--from the moment of conception.</P>
<P>Thompson tried to claim that the proposal was merely a way of guaranteeing prenatal health care for pregnant women. "All we're doing is providing care for poor mothers so their children are going to be born healthy," Thompson said. "How anybody can now turn this into a pro-choice or pro-life argument, I can't understand it."</P>
<P>What a liar! Right-wingers celebrated Thompson's announcement because the proposal would allow the government to claim that a fetus has the same rights as a person.</P>
<P>"If they're interested in covering pregnant women, why don't they just talk about pregnant women?" asked Laurie Rubiner of the National Partnership for Women and Families. "I just have to believe their hidden agenda is to extend personhood to a fetus."</P>
<P>In fact, the Department of Health and Human Services allows states to cover pregnant women under CHIP, without having to classify fetuses as "children." Thompson now claims that getting permission for this takes too long. But the real point of his proposal is to open the door for an even tighter clampdown on abortion rights.</P>
<P>"At the point you establish a fetus is a person under the law, then even first trimester abortion becomes murder, and the Bush administration knows that," said Kim Gandy, president of the National Organization for Women. "Endowing a fetus with more rights than a pregnant woman is more than a back-door attempt to restrict abortion rights. It's also a slap in the face to women everywhere."</P>
<P>Ever since the Bush gang took over the White House last year, they've chipped away at abortion rights. This new proposal shows that they think they can go all the way.</P>
<P>We can't let them get away with it. We have to build the fight to defend our abortion rights.</P>

