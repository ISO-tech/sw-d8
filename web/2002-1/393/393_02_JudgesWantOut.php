<P><font face="Times New Roman, Times, serif" size="5"><b>Judges want out of Justice Department</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 8, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>EVEN U.S. judges want out of John Ashcroft's Justice Department. Some 200 judges who preside over immigration cases are angry at executive orders, imposed by Ashcroft since September 11, that limit their powers.</P>
<P>For example, on October 31, Ashcroft issued a rule allowing the Immigration and Naturalization Service (INS) to override the judges' decisions to release immigrants on bond if they were detained in investigations into the September 11 attacks.</P>
<P>In a new report, the National Association of Immigration Judges--an AFL-CIO-affiliated union that represents the judges--calls on Congress to establish a new agency independent of the Justice Department. "It is not surprising that the public perceives this system as 'rigged,'" the report concludes.</P>

