<P><font face="Times New Roman, Times, serif" size="5"><b>You can't ever trust Democrats</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 8, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Lance Selfa's recent column, "Why won't the Democrats fight?" (<I>SW</I>, February 1), made all the right arguments as to how the Democrats look like the less terrible twin of the Republican Party and how they are corrupt to the core with money and influence from corporations.</P>
<P>Unfortunately, Selfa also left one point in the shadows. Reading the article, a person could believe that if people fight, we could have a return to the "old-style" Democrats of Franklin Roosevelt, who fought on the side of workers. But Roosevelt was no friend of workers.</P>
<P>We must stay independent even when the Democrats put on a compassionate mask.</P>
<B><P>Douglas Murray,</B> Amherst, Mass.</P>

