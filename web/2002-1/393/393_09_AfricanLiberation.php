<P><font face="Times New Roman, Times, serif" size="5"><b>Africa's struggle for independence</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 8, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>ART: <I>The Short Century: Independence and Liberation Movements in Africa, 1945-1994</I>.</B> P.S.1 Contemporary Art Center and the Museum of Modern Art, New York, February 10-May 5, 2002.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>THE NEARLY half a century following the Second World War to the 1994 fall of the apartheid regime in South Africa transformed Africa.</P>
<P>When the Second World War ended, major European powers maintained colonies throughout Africa. By 1994, all African countries had won their liberation from European colonialism and its vestiges.</P>
<I><P>The Short Century</I>--a multimedia exhibit that includes documentary film, paintings, political posters, collages and sculpture--marks this important period. The exhibit shows the varied ways that African artists captured the spirit of resistance to colonialism.</P>
<P>The art reflects the challenge that faced African artists of the "independence" generation--between adapting artistic styles developed outside Africa to the African context and trying to develop an indigenous art that expressed the hopes for the newly independent countries.</P>
<P>For many, <I>The Short Century</I> will introduce the movements and figures--Pan-Africanism, Kwame Nkrumah, Gamel Abdel Nasser, the Algerian civil war, Amilcar Cabral, the African National Congress (South Africa)--that dominated post-Second World War African politics.</P>
<P>In "The History of Zaire," Congolese painter Tshibumba recounts the rise and fall of Patrice Lumumba, the national leader of the newly independent nation of Congo in the early 1960s. In paintings reminiscent of Mexican muralist Diego Rivera, Tshibumba shows Lumumba leading the movement for independence from Belgium, his CIA-engineered murder, and the military coup that installed dictator and U.S. stooge Mobutu Sese Seko.</P>
<P>Resistance to apartheid in South Africa inspired some of the exhibit's most striking pieces. Sue Williamson's 1990 "For Thirty Years Next to His Heart" covers a 30-foot-high wall with photographs of a single South African worker's passbook, the identification papers the apartheid government required all Blacks to carry. It is a stark reminder of the state-mandated oppression that Blacks endured under apartheid.</P>
<P>A more directly political attack on apartheid is carried in Gavin Jantjes' scathing "South African Colouring Book, 1974-1975," a collage of photos, drawings and reproductions of official government statements. It dismantles the ideology of apartheid, with one panel juxtaposing statements of former South African Prime Minister John Vorster and Adolph Hitler. You can't tell the difference between them.</P>
<I><P>The Short Century</I> is both an art exhibit and a work of social and political history. It makes this connection explicit through continuously playing video documentaries of political events--from the Algerian civil war to the 1976 Soweto Uprising of school children against the apartheid regime. One section displays political posters of the independence struggles in Angola, Mozambique, Guinea-Bissau and the anti-apartheid struggle.</P>
<I><P>The Short Century</I> pays tribute to the hopes that the African liberation movements unleashed. With Western governments blithely talking about "failed states" and a "new colonialism," <I>The Short Century</I> reminds us of the struggles that defeated the "old colonialism."</P>

