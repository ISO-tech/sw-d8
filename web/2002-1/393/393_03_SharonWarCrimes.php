<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Regrets of Israel's top war criminal</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 8, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ON THE eve of his meeting with George W. Bush, Israeli Prime Minister Ariel Sharon discussed one his life's great regrets. He wished that he had "liquidated" Palestinian leader Yasser Arafat when he had the chance--during Israel's 1982 invasion of Lebanon, he said.</P>
<P>You might think that such casual talking about assassinating Arafat would draw some criticism from the White House. Not so.</P>
<P>Bush has given Israel the green light to do virtually anything that it wants to repress, humiliate and murder Palestinians. And Israel has taken full advantage.</P>
<P>Its savage assault on the 3 million Palestinians in the Occupied Territories has escalated massively. Under the guise of rooting out terrorism, the Israeli government has demolished hundreds of houses, assassinated dozens of Palestinian leaders and occupied whole towns in clear violation of agreements that it signed in the past.</P>
<P>Whenever the confrontations grow quiet for any length of time, Israel commits an atrocity guaranteed to provoke a response from Palestinians. Days before Sharon was to arrive in Washington, Israel was up to its old tricks--killing four Palestinian activists in Gaza in a missile attack on their car.</P>
<P>The attack came a day after Arafat pledged to sit down with any Israeli leader to negotiate "freedom for the Palestinians, a complete end of the occupation, security for Israel and creative solutions to the plight of the [Palestinian] refugees while respecting Israel's demographic concerns."</P>
<P>These are major concessions by Arafat. But as usual, Sharon dismissed them as "irrelevant." That's because he only cares about continuing his war against the Palestinian people.</P>
<P>Yet a small number of Israelis have begun to question his plans. Israel's cruel demolition of Palestinian homes in Gaza last month sparked a debate in the Israeli media about whether the action constituted a war crime.</P>
<P>Even more significantly, 53 prominent military officers publicly called on Israeli soldiers to refuse to serve in the Occupied Territories "for the purposes of domination, expulsion, starvation, and humiliation of an entire people."</P>
<P>"[Y]ou are asked to do things that should not be asked of you, to shoot people, to stop ambulances, to destroy houses in which you don't know if there are people living," said Reserve Lt. David Zonshein.</P>
<P>This movement is small. But it could grow to help expose the Israeli war criminals that the U.S. government won't even criticize.</P>

