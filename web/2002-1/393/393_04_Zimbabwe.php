<P><font face="Times New Roman, Times, serif" size="5"><b>Zimbabwe socialists under attack by regime</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 8, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>The repression against opposition activists in Zimbabwe has grown worse. Since President Robert Mugabe signed a so-called antiterrorist law, the Public Order and Security Act (POSA), on January 18, we have witnessed a further crackdown on democracy--harassment, disruption of opposition meetings, beatings and arrests of activists.</P>
<P>Socialists are also being targeted. On January 28, an organizer for the International Socialist Organization (ISO) in Zimbabwe, 59-year-old Elliot Madzivanyika, was ambushed in a working-class township of Mbare by militia from Mugabe's ZANU-PF. He was then handed over to police and charged with violating POSA for insulting the president.</P>
<P>This incident followed the arrest of another ISO member, Jokonia Maopa. The same charges were fabricated against him. Both will go on trial in mid-February. If convicted, they face severe penalties under POSA of up to 10 years in jail or a huge fine.</P>
<P>We have teamed up with organized labor and other radical civic groups to mobilize for action under the theme, "No to dictatorship, no to neoliberal poverty." We have set February 15 as a day of mass action to send a message to the regime.</P>
<P>We are asking for solidarity messages and action from workers, socialists and others, as the ZANU-PF regime is likely to go all-out to smash this action. We are also asking for financial support to assist with the cost of defending ourselves.</P>
<B><P>A reader in Zimbabwe</P>
</B><I><P>Messages of solidarity and contributions can be sent care of Socialist Worker, P.O. Box 16085, Chicago, IL 60616. Send e-mail messages care of reports@socialistworker.org.</P>
