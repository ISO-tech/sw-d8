<P><font face="Times New Roman, Times, serif" size="5"><b>Delta bosses defeat union organizing drive</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Leonard Klein</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 8, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>DESPITE THE fact that 98 percent of flight attendants who turned in ballots in the recent election for union representation at Delta Airlines voted "yes," federal election rules turned the vote into a defeat on February 1.</P>
<P>Of the 5,609 ballots cast, 5,520 attendants voted to join the to the Association of Flight Attendants (AFA). But because of antidemocratic Railway Labor Act election laws, 13,424 ballots not sent in counted as "no" votes.</P>
<P>Under National Mediation Board (NMB) rules, unions seeking certification must receive 50 percent plus 1 votes of all eligible bargaining unit members. So, despite overwhelming support for the union, Delta remains the only major airline without unionized flight attendants.</P>
<P>Delta spent millions defeating the AFA's four-year effort. Management harassed pro-union workers and hired American Consulting to run an anti-union campaign, which used the threat of layoffs in the wake of September 11 to intimidate workers. After "furloughing," or temporarily laying off, 3,000 flight attendants, Delta told them they couldn't vote--a bald-faced lie.</P>
<P>The NMB has found that Delta interfered with the organizing drive and election. AFA leaders hope the NMB's investigation will lead to a special election, bypassing the usual one-year waiting period for another vote.</P>
<P>The AFA is also requesting that the NMB issue a "Laker" ballot election, which requires only 50 percent plus 1 of votes <I>cast</I> to win. An announcement is expected in 60 days. Unfortunately, AFA International President Patricia Friend said that if no "Laker" election is called, the AFA will abandon the drive.</P>
<P>The defeat at Delta shows the extent to which George W. Bush and his corporate buddies are stacking the deck against workers, and their willingness to use the September 11 tragedy as an excuse to make workers pay for their continued profits.</P>
<P>Even more, it shows why labor activists have to begin organizing a fighting opposition--from the bottom up--to stop Bush's assault on our unions.</P>

