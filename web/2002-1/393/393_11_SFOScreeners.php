<P><font face="Times New Roman, Times, serif" size="5"><b>San Francisco airport screeners</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 8, 2002 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>UNDER THE Aviation and Transportation Security Act, thousands of immigrants across the country will lose their jobs.</P>
<P>In the wake of September 11, Congress passed the anti-immigrant act, requiring that all airport baggage screeners are U.S. citizens by November 19, 2002. At San Francisco International Airport (SFO), approximately 800 of 1,200 baggage screeners are immigrants, and 80 percent are from the Philippines.</P>
<P>Workers at SFO--one of only two unionized airports in the country--are gearing up for a February 19 action to protest the new law and save their jobs.</P>
<P>ERLINDA VALENCIA--a screener at SFO for 14 years and a shop steward for Service Employees International Union (SEIU) Local 790--spoke to <I>Socialist Worker</I>'s EDUARDO CAPULONG and MIKE ESTRADA.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<B><P>WHAT'S HAPPENING at SFO?</P>
</B><P>THEY ARE targeting all the immigrant workers. These immigrant workers have been working here for several years, so they're already trained, screened, and they know their job.</P>
<P>Learning to do the job takes a year or more. It requires a lot of experience and training, so placing citizens who are new [to the job] is not the solution to the problem.</P>
<P>U.S. citizens don't like this job because it pays minimum wage. It was only $4.25 when I started. After six months, I made it to $4.50 because I worked hard and knew the job. I worked 12 hours a day, six days a week to make ends meet.</P>
<P>Before the union, you could see how management used favoritism to reward screeners close to them. But when the union came, they changed. They're now trying their best to treat everybody equally.</P>
<P>We're very thankful the union came. I'm getting $15 an hour now as a computer X-ray operator.</P>
<B><P>HOW IS the new law affecting workers at the airport?</P>
</B><P>EVERYBODY'S SCARED. I have a friend there. She keeps on working, doing overtime. She says, "I'm saving money because I know I'm going to be laid off. I can't apply to be a citizen because I've only been here a year." She knows that if the law is implemented, she'll be out.</P>
<P>The truth is that they're all depending on the union. When [my coworkers] learned that we're going to do an action to keep our jobs, they thanked me. We all work together.</P>
<P>I'm sure we're not going to be laid off. We'll keep our jobs.</P>
<P>I want [your readers] to understand that we immigrant workers, although we are immigrants, have a deep concern for the lives of the flying public. And we would like them to support us because we are doing our best to make the operation safe.</P>

