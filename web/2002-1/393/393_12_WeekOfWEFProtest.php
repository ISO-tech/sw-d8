<P><font face="Times New Roman, Times, serif" size="5"><b>A week of protests in New York</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 8, 2002 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>January 30<BR></B><BR>
About 200 people attend a panel called "Fighting Corporate Globalization Since 911" sponsored by the International Socialist Organization (ISO). Speakers include Frances Fox Piven, author of <I>Poor People's Movements</I>; investigative journalist Gregory Palast; Michael Letwin of New York City Labor Against the War; and ISO member Jennifer Roesch.</P>
<B><P>January 31<BR></B><BR>
The AFL-CIO's indoor rally draws a crowd of 400, and 1,000 people--members of UNITE and other unions--march on a Manhattan Gap store.</P>
<P>Students for Global Justice begin their two-day conference at Columbia University.</P>
<P>Some 350 people hear <I>Democracy Now!</I> host Amy Goodman, author Manning Marable and other speakers at an evening plenary.</P>
<P>A roundtable discussion on strategies for the global justice movement, with speakers Jaggi Singh from the Montreal Anti-Capitalist Convergence, Mike Dolan from Public Citizen, and ISO member Katherine Dwyer, draws 80.</P>
<B><P>February 1<BR></B><BR>
At the Columbia conference, more than 400 people attend the evening panel on "Another World Is Possible: Globalizing Justice and Solidarity."</P>
<P>A spokescouncil meeting to discuss the weekend's actions draws more than 400.</P>
<B><P>February 2<BR></B><BR>
About 15,000 gather at the south end of Central Park and march on the World Economic Forum two miles away. Though police arrested more than 30 people near the back of the march, the expected confrontation as protesters neared the "frozen zone" around the Waldorf didn't take place.</P>
<B><P>February 3<BR></B><BR>
Smaller direct-action protests take place across the city.</P>
<P>Police target protesters and arrest more than 100 people for infractions like jaywalking.</P>
<B><P>February 4<BR></B><BR>
More than 200 people gather outside Arthur Andersen's New York headquarters to speak out about the Enron scandal holding signs like "WEF=Wasn't Enron Fun?" and "Andersen cooks Enron's books!"</P>

