<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 8, 2002 | Page 11</font></p>

<P><font face="Times New Roman, Times, serif" size="4"><b>Portland nurses</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul Dean</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>PORTLAND, Ore.--Nurses at Oregon Health and Science University (OHSU) Hospital have now been on strike more than 50 days. Negotiations resume on Monday after a lull of two weeks.</P>
<P>The state labor board has already determined that OHSU has broken the unfair labor practice law by paying an extra $7.50 an hour to Oregon Nurses Association (ONA) members who cross the picket line.</P>
<P>OHSU officials have been trying everything to project the image that everything's normal, but they can't hide the fact that the scab nurses are failing to provide adequate care. Many stories are coming out of the hospital--like patients being sent out to have IV's inserted at other hospitals, and scab nurses doing work they weren't trained for or trying to persuade women not to have abortions!</P>
<P>"You are the heart of OHSU," one man told striking nurses as he was picking up a prescription only available at OHSU. "The reason they are called scabs is because when you guys went on strike, they left open wounds."</P>
<P>Stew--a nurse at OHSU for 21 years--told <I>Socialist Worker</I> that management assertions that the nurses have "Cadillac benefits" may have been true in 1991, but now they're just buying Volkswagens.</P>
<P>Official pronouncements that the strike is having a minimal effect are nonsense. "It ain't pretty at all," said Stew.</P>
<P>Laurie has worked at OHSU for 19 years. She said scab charge nurses get a $10 an hour differential. "I usually get a $1 an hour differential as a charge," she told <I>Socialist Worker</I>. "When we go back, we'll be back to $1."</P>
<P>"I've lost faith. I've been a dedicated nurse here, and I'm surprised at the lack of respect. They think that we are so easily replaced, but there's someone up there doing my job that isn't doing it as well as me."</P>
<P>Doctors and anesthesiologists are doing jobs normally done by nurses--and receiving doctor's wages of course. Nurses say if the doctors spoke out about what is going on in the hospital, it would shorten the strike. But doctors are scared that their contract won't be renewed, and there are rumors that they could be getting big bonuses.</P>
<P>It seems management will spare no expense to beat the nurses--and they are spending at least $500,000 a week in the effort. But nurses around the state--whose wages and conditions will be impacted by the outcome of this strike--are giving the ONA nurses financial and moral support.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Krug Winery</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Ken Morgan, ILWU Local 6</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ST. HELENA, Calif.--Production workers at Charles Krug Winery, who have been locked out for more than six months, voted 24-13 to reject the company's latest contract offer on January 7.</P>
<P>Krug is the only unionized winery in Napa County, which is the center of the California wine industry. The members of United Food and Commercial Workers are picketing St. Helena winery and area stores while scabs continue production.</P>
<P>It was the second time workers voted on a contract offer since being locked out July 3. Every worker but one rejected the winery's August offer. "It wasn't really possible to take it seriously," Kenny Drost, a cellar worker at Krug for 21 years, said of the company's latest proposal. "They're fighting for their dignity as much as their jobs."</P>
<P>Negotiations between Krug management and the Modesto-based winery workers' union have barely progressed since the company stalled after the workers' last contract expired in April, leading to the July lockout. Management is demanding pay and job cuts as a condition to ending the lockout.</P>

