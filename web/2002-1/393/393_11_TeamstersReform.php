<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The fight for real Teamsters reform</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By a member of Teamsters Local 710</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 8, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"WE HAVE learned that to get things done in Washington, you have to get support from both sides of the aisle." That was International Brotherhood of Teamsters President James P. Hoffa's excuse for throwing a January 23 bash for guests that included House Speaker Dennis Hastert (R-Ill.) and Labor Secretary Elaine Chao.</P>
<P>Why would Hoffa wine and dine these enemies of labor? Hoffa claims the reason is jobs--that Bush's proposed drilling in the Arctic National Wildlife Refuge (ANWR) would create 735,000 jobs, 25,000 for Teamsters. But environmental groups contend the overall job figure would be less than 50,000--and the damage to ANWR extreme.</P>
<P>Hoffa's real motivation is to end government oversight of the Teamsters, the result of a 1989 consent decree in federal court.</P>
<P>Bush repaid Hoffa for supporting ANWR drilling by eliminating the government's Independent Financial Auditor (IFA) office that oversees the Teamsters' books. The union agreed to this oversight after the 1996 scheme that aides to former Teamsters President Ron Carey used to funnel union money into Carey's re-election campaign.</P>
<P>In a Republican-led witch-hunt aided by Hoffa, Carey--who led the popular 1997 strike against United Parcel Service (UPS)--was unseated and barred from the union. This forced an early election in 1998, won by Hoffa over reformer Tom Leedham.</P>
<P>Government overseers allowed Hoffa to run despite his own fundraising violations. The International Election Officer found the Hoffa administration diverted union funds to Hoffa's campaign in 2001 in a second election victory over Leedham. Meanwhile, a federal court cleared Carey of all charges in October 2001--meaning Hoffa, like Bush, is an illegitimate president.</P>
<P>And Hoffa hasn't simply been able to turn back the clock. His delegates at the June 2001 convention put democratic reforms into the constitution, but they also increased expense money for Teamster staffers while defeating a proposal to adequately fund a strike fund. Now Hoffa is proposing a special convention this spring to address the financial situation in the union--and consolidate his power.</P>
<P>But there's a showdown ahead that will put Hoffa to the test. The Teamsters' contract with UPS expires July 31, 2002. Hoffa is under pressure to deliver for UPS workers, but Hoffa's 57-member contract negotiating team is woefully short of rank-and-file UPS members and stacked with local officers, at least 20 of whom make between $100,000 and $225,000 a year.</P>
<P>This is a far cry from Carey's UPS campaign, which stressed member involvement. Under the Carey administration, Teamsters for a Democratic Union (TDU) used the opportunity to help get reform candidates elected or appointed to local union offices.</P>
<P>But the limits of electing reformers were seen following Carey's ouster. For example, former Carey ally George Cashman, president of Local 25 in Boston, switched to Hoffa and now faces embezzlement and bribery charges.</P>
<P>Former Parcel Division head Ken Hall, who ran briefly against Hoffa after Carey was ousted, is now on Hoffa's UPS negotiating team. In Chicago, Local 705 leader Gerald Zero, a one-time poster boy of reform, jumped to the Hoffa camp early in 2001.</P>
<P>What's needed is a bottom-up approach that mobilizes rank-and-file union members to become the key fighters for union democracy. This means a return to the tactics of the 1930s and 1970s that saw a number of wildcat strikes by Teamster members--bucking the express wishes of the International leadership.</P>
<P>This kind of movement depends on members' ability to stop production--and the bosses' profits--at the source.</P>
<P>The UPS contract can be a focus for this reform effort. UPS workers need to mobilize to hold Hoffa to his word that there will be no givebacks to UPS--and turn the contract campaign into a real rank-and-file initiative that can take on Big Brown.</P>

