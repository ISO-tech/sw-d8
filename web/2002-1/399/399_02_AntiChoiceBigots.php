<P><font face="Times New Roman, Times, serif" size="5"><b>Anti-abortion bigots' offensive in Virginia</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 22, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ABORTION RIGHTS are under attack again, this time in Virginia. On March 7, the Virginia Senate passed a law that would ban the late-term abortion procedure known as intact dilation and extraction--misnamed "partial-birth abortion" by anti-choice fanatics.</P>
<P>The Virginia bill calls the procedure "medically induced infanticide," and lawmakers stripped out a provision to allow exceptions to the ban in cases where a woman's health is threatened.</P>
<P>Coming on the heels of recent laws that create a mandatory waiting period for women seeking abortions and require parental notification for women under the age of 18, the new bill is another step toward making abortion illegal. Robert Marshall, the Virginia Republican who sponsored the bill, admitted as much when he told reporters, "If it's one abortion, it's too many."</P>
<P>But this attack on a woman's right to choose won't stop Virginia's Democratic Gov. Mark Warner from signing it. Warner has repeatedly told the media that he will sign a ban on so-called "partial-birth" abortions--if it will hold up in court.</P>
<P>That's a far cry from the rhetoric that Warner used to get into office. When he was running for governor in 1996, he won support from pro-choice voters by insisting that he "[trusts] the women of Virginia with this most personal of decisions."</P>
<P>We can't trust spineless Democrats to defend our right to choose.</P>

