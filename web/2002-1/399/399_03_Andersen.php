<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Andersen headed for the shredder?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 22, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WILL THE corporate con artist that covered up for the Enron gang go belly-up?</P>
<P>The accounting giant Arthur Andersen was teetering on the brink after it was indicted in federal court for obstruction of justice--more precisely, the destruction of thousands of documents and e-mails related to its audit of Enron. Within hours, the stream of big-time corporations dumping Andersen became a torrent.</P>
<P>As Enron's auditor, you might think that Andersen was supposed to be uncovering the book cooking that pulled down the energy company. But Andersen was also paid as a "consultant"--to help Enron come up with the very accounting tricks that it okayed as an "auditor." They may snipe at each other today, but Enron and Andersen bosses were thick as thieves.</P>
<P>The Enron-Andersen scandal has dropped off the front pages, with the mainstream media content to dismiss them as rotten apples. But don't think for a second that this is an isolated case. Every big accounting firm has deals with major corporations to be both consultant and auditor.</P>
<P>The industry went to the mat to defend this shell game two years ago, when former Securities and Exchange Commission (SEC) chair Arthur Levitt proposed legislation that would have forced firms to be one or the other.</P>
<P>Andersen and friends lined up a bipartisan pack of attack dogs--led by Sens. Charles Schumer (D-N.Y.) and Phil Gramm (R-Texas) and Rep. Billy Tauzin (R-La.)--to force Levitt to back down.</P>
<P>Likewise, the "if in doubt, shred it" rule at Andersen is hardly unique. "Ask executives and employees to imagine all their documents in the hands of a zealous regulator or on the front page of the <I>New York Times</I>," a top accounting industry lawyer declared in a 1994 law review article. "Each company should have a system of determining the retention and destruction of documents."</P>
<P>Andersen bosses could be forgiven for expecting some sympathy in Washington for following this point of view. After all, the man who advised them to head for the shredders was Harvey Pitt--the current chair of the SEC, appointed last year by George W. Bush.</P>
<P>Andersen may be a rotten apple, but the whole system is corrupt.</P>

