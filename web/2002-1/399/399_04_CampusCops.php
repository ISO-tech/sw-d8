<P><font face="Times New Roman, Times, serif" size="5"><b>Campus cops show their racism</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 22, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Two Black students at Brown University recently were assaulted by Brown cops. The students were followed for a while, until they were asked for their IDs. One of the students, recognizing that he was being racially profiled, refused to give his ID.</P>
<P>The officer then called for backup and seven officers rushed to the scene, forcing the student to the ground and handcuffing him--despite the fact that there were 50 students looking on.</P>
<P>The excuse the police gave was that there has been vandalism around campus, and that these students looked "suspicious."</P>
<P>This year, because of an apparent spike in crime, there has been a push to arm the Brown cops. So campus activists have formed the Coalition Against Guns at Brown (CAGB).</P>
<P>No one believed us when we told stories of people who had been harassed by Brown cops, but this incident has opened many eyes.</P>
<P>The fact that the police did this in front of so many witnesses shows how arrogant the police have gotten after September 11. We have to show them that we won't let them get away with it.</P>
<B><P>Brian Rainey,</B> Providence, R.I.</P>

