<P><font face="Times New Roman, Times, serif" size="5"><b>Bosses using September 11 to control lives</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 22, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Our country is moving closer to a police state. ChoicePoint, a company that does background checks for corporations, reports that the number of checks it does in a month has increased from 3,000 to nearly 25,000 in February.</P>
<P>One company ChoicePoint services is Eli Lilly, a pharmaceutical manufacturer, which began doing background checks on employees of outside contractors after September 11. Lilly has banned a number of workers based on these checks.</P>
<P>It banned one man who had been convicted of possessing marijuana nine years earlier. It also banned a single mother who had once bounced a check for $60. Apparently the logic here is that if you have bounced a check, you could be a terrorist.</P>
<P>There also have been increasing complaints about the new security measures at airports. The <I>Los Angeles Times</I> reports that there have been dozens of complaints from female airline passengers about being groped and subjected to demeaning sexual remarks by male security screeners.</P>
<P>At a Phoenix airport, an 86-year-old man was repeatedly searched after screeners found a "suspicious article" on him. It turned out to be a World War II Medal of Honor.</P>
<P>At a Florida airport, screeners took aside a three-year-old girl for a manual scan. The child's father recalled, "There was no acknowledgment that this was silly exercise. He said he'd done several 3-year-olds that day."</P>
<P>Big business and the government are using terrorism as an excuse to exert greater control over our lives. We can end this intimidation, but only if we fight back.</P>
<B><P>Evan Kornfeld,</B> Los Angeles</P>

