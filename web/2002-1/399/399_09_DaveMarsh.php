<P><font face="Times New Roman, Times, serif" size="4">Critic Dave Marsh on politics and music:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"It's essential that musicians respond"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 22, 2002 | Page 9</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>DAVE MARSH is a veteran music journalist who has written for <I>Rolling Stone</I> and <I>Creem</I> magazines. He's also the author of a dozen books, including <I>Born to Run: The Bruce Springsteen Story</I>. In 1983, Marsh founded the newsletter <I>Rock and Rap Confidential</I> as a non-corporate voice to focus on the relationship of music and politics.</P>
<P>Marsh talked to NICOLE COLSON about music and the music industry in the face of George W. Bush's war drive.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<B><P>WILL POLITICAL musical acts be able to continue expressing themselves with George W. Bush and John Ashcroft warning them to "watch what they say"?</P>
</B><P>SURE THEY<I> can</I>. But as my friend Harold Leventhal, who shepherded the Weavers through the McCarthy blacklist period, pointed out recently, they just can't any longer expect that they will do that with the same assurance of impunity.</P>
<P>That isn't the exclusive province of Ashcroft and the Bush apparatus either. Wait and see how long it takes for Susan Sarandon to get another high-profile movie. That's retribution for backing Nader (and opposing Gore/Lieberman, who are at least as big a horror show when it comes to free expression).</P>
<P>There has always been a tremendous amount of slack in the American media, and a tremendous lack of interest when that slack begins to be pulled in.</P>
<P>The question is how we can make loud, effective noises to advance positive programs and values, without being intimidated into either silence or ineffective compromise by the reality that there are plenty of people in powerful places who think that "tolerance" means "at least we didn't line 'em up and shoot 'em."</P>
<B><P>CAN MUSICIANS get around the enforced patriotism that's out there right now?</P>
</B><P>YOU CAN'T "get around" it. You either deal with it, or you ignore it. I've never thought that ignoring what was happening was a particularly good option, anyway.</P>
<P>I think it's essential to respond, if only because there are people out there who need a response--to show them that they can have one, too.</P>
<P>There are also people out there who believe that everyone they care about--artists they like included--accepts the hegemony and imperialism of America. And why wouldn't they think that if nobody is challenging it?</P>
<B><P>WHAT IMPACT do you think radical musicians can have on political movements?</P>
</B><P>THAT'S WHAT my next book, a musical history of the civil rights movement, is about in a way. The answer is simple at one level: If there is a movement, there will be music involved, and it may even have a very central role to play, as it did in the civil rights movement.</P>
<P>But you ask about musicians, and that's a whole other kettle of fish--because it may not be professional musicians who make the music of the movement. Fannie Lou Hamer was a great singer, but "singer" wasn't her job, or her self-identification, either. If you ask the SNCC [Student Non-violent Coordinating Committee] Freedom Singers, they will tell you proudly that music for them was a way of organizing. Only one or two of them make their livings as singers, even now.</P>
<P>Movements rise up out of people's need to express demands that the present society cannot or will not meet. Musicians aren't exactly dragged along in their wake, but it would be a very unusual movement that musicians could <I>start</I>--which is (I think) the expectation of many on the left right now.</P>
<P>I would love to see someone write the 21st-century equivalent of <I>Uncle Tom's Cabin</I>. But absent a movement to galvanize a writer into writing such a book--or movie or whatever--that isn't going to happen. And then again, you go see a movie like <I>John Q</I>, and you wonder if there isn't already something out there, waiting to be galvanized.</P>
<P>Health care isn't the worst bet for what could really get things started in that direction. But not if it's going to lead to bickering with Congress about how little we can get--not if it's going to be trying to figure out how to convince Ted Koppel that the insurance industry propaganda barrage is wrong.</P>
<P>Only if it somehow empowers people, gives them the sense that <I>they</I> can change their own lives--in company with the people who share those lives.</P>
<B><P>WHO DO you consider to be today's best political musicians?</P>
</B><P>I WANT more than decent musicianship. I want more than good politics. I want music that's as good as it can be, and people who have a real deep involvement in issues and struggles, and people who are trying to change things fundamentally through such involvement.</P>
<P>The first three people who came to mind were Willie King, the Southern blues singer, who is deeply immersed in a culture where the civil rights movement never quite ended; Billy Bragg, who set out to be a living embodiment of the socialist and humanist principles he's involved with; and Steve Earle, whose work against the death penalty and for poor people is endless.</P>
<P>Boots from the Coup means what he says and fights to keep saying it. He's what you might call a leveler--he isn't arguing that entrepreneurial endeavor is gonna change the face of things, or the heart of them, ether.</P>
<P>Mos Def, Dead Prez and a whole roster of hip-hop guys are beginning to think along similar lines.</P>

