<P><font face="Times New Roman, Times, serif" size="5"><b>Washington loves a dictator</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 22, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WHAT DO you get for letting the U.S. military use your country as an air base? A free pass to torture your citizens.</P>
<P>Last week, Secretary of State Colin Powell welcomed Uzbekistan's President Islam Karimov to Washington, D.C., with open arms.</P>
<P>The Bush administration admits that Karimov's government is one of the most brutal and repressive in the world. The State Department's annual report on human rights--released less than two weeks before Karimov showed up--called Uzbekistan "an authoritarian state with limited civil rights."</P>
<P>"The Government does not permit the existence of opposition parties," reads the report. "Security force mistreatment resulted in the deaths of several citizens in custody&#133;The security forces arbitrarily arrested and detained persons, on false charges, particularly Muslims suspected of extremist sympathies, frequently planting narcotics, weapons or banned literature on them."</P>
<P>But that didn't stop the administration from rolling out the red carpet for a man who recently extended his term of office to 2007 through a sham referendum. After all, immediately after September 11, Karimov granted permission for the U.S. to station troops at his air base in Hanabad.</P>
<P>He knew the action would pay off for him big time. The U.S. Export-Import Bank recently granted a $55 million credit guarantee to Karimov--and U.S. aid to the country tripled to $160 million.</P>
<P>No wonder Karimov told reporters: "The United States may remain in Uzbekistan as long as they think it is necessary."</P>

