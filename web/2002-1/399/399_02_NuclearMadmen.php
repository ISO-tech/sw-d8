<P><font face="Times New Roman, Times, serif" size="4">Bush gang's new chapter to atomic horror story</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Nuclear madmen in the White House</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 22, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH administration has gone from MAD to worse. For decades, the aptly named concept of "MAD"--Mutual Assured Destruction--was the supposed reason why there wouldn't be a nuclear war between the U.S. and former USSR.</P>
<P>The huge nuclear arsenals of the Cold War superpowers would "deter" the use of nukes, went the argument, because any first strike would be met by an apocalyptic counterattack.</P>
<P>But the Bush gang has different ideas. The Pentagon's Nuclear Posture Review, finished earlier this year, is a blueprint for carrying out a nuclear first strike.</P>
<P>The Pentagon report advocates development of a new kind of nuclear weapon for the 21st century--a so-called "mini-nuke" that could be targeted at underground bunkers or protected stockpiles of biological or chemical weapons. In other words, a "usable" nuclear weapon.</P>
<P>The proposal violates international arms control agreements, but that isn't stopping the Pentagon. "That old process is incompatible with the flexibility U.S. planning and forces now require," its report declares.</P>
<P>No one in their right mind could be less than terrified by the Bush gang's nuke-happy scheme. But this is only the latest chapter in the U.S. nuclear weapons horror story:</P>
<P>--The U.S. is the only power ever to use these weapons of mass destruction--against Japan at the end of the Second World War.</P>
<P>--Since 1945, the U.S. has threatened to use nuclear weapons 16 times that we know of. The most famous instance was during the Cuban missile crisis of 1962, but the U.S. also considered using nukes against Vietnam, China and even USSR forces in East Germany.</P>
<P>--The U.S. has spent more than $5.5 trillion to develop, produce, deploy and maintain nuclear weapons since 1940--almost $20,000 for every man, woman and child living in the U.S. today.</P>
<P>--The U.S. has detonated 1,125 nuclear devices in tests. In 1952, the U.S. vaporized an entire island and part of two more in the Pacific during a weapons test.</P>

