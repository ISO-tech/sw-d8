<P><font face="Times New Roman, Times, serif" size="4">States slash budgets for our schools</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>We can fight these cuts</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 22, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>BUDGET CUTTERS in cities and states across the U.S. are getting a message from protesters: We're not going to take it.</P>
<P>That was the news that hundreds of Chicago's city college students had for Mayor Richard Daley last Thursday in a protest against plans to abolish counseling services. The 19 counselors--who serve more than 70,000 students in the two-year city college system--are crucial for helping students choose the right classes to be able to transfer to four-year schools.</P>
<P>"A lot of students don't take the right classes, and they end up like me--staying at Harold Washington [College] longer than they need to be and paying more money than they have to," student Raven Cox said, as the mostly African American and Latino protesters chanted at cops who barred the doors to City Hall. "I think it shows that they're not interested in education. They're more interested in the wars or the jails."</P>
<P>Another student, Jonathan Pettis, agreed. "It's all about the rich getting richer, and the poor getting poorer," he said. "If they can hold you back in mental slavery, this is the ideal of control. We as African American students may not be in chains and bondage again, but if they can control what I learn, what I read and how far I can be educated, it is a dictatorship, regardless of what this system calls itself."</P>
<P>There are similar scenes taking place in cities and towns across the country. In Boston, furious teachers and parents packed a meeting of the Boston School Committee, where officials wanted to ram through a $41 million budget cut that would eliminate public transportation passes for kids and lay off health paraprofessionals.</P>
<P>In Detroit, school board officials planned to keep their March 20 meeting closed to avoid a repeat of last month's session, where parents and union members shut down proceedings over plans to eliminate 700 jobs.</P>
<P>In fact, 36 states have already planned or implemented cuts to meet budget shortfalls that could reach $100 billion next year, according to the Center on Budget and Policy Priorities. Much of the blame lies with George W. Bush, who is slashing social spending to pay for tax cuts for corporations and the wealthy and a massive military budget.</P>
<P>But Democrats and Republicans alike are pushing the cuts at the state and local levels. And after years of pious talk from Washington about the importance of education, public schools are taking some of the most outrageous cuts of all.</P>
<P>The protests, pickets and meetings against these cuts are still in their early stages. And because of a blackout by corporate media, these sparks of resistance may remain hidden.</P>
<P>But in each one, a common theme can be heard. We don't want these cuts to wreck our children's futures. And we're going to fight to stop them.</P>

