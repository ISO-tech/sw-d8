<P><font face="Times New Roman, Times, serif" size="4">Telling the truth about the Palestinian struggle</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Standing our ground</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 22, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>It's been so long since anyone's called me a "nigger" to my face.</P>
<P>The other night, I was hanging out at my local bar with some comrades, and I felt a tap on my shoulder. "Take off that fuckin' scarf" I was told by my new "best friend," Billy.</P>
<P>Billy is an iron worker and a union man, but also, unfortunately, a racist. He took offense at my scarf, the keffiyah, a traditional Palestinian scarf.</P>
<P>"Are you Arab?" he asked. I told him I'm Black, I was born in Detroit. "Then take off the fuckin' scarf!" he said. Turns out Billy didn't really want to discuss it. He wanted to fight.</P>
<P>Billy was full of anger, but it was contradictory. He had worked at Ground Zero, struggling to find the human remains in an enormous pile of twisted steel and metal. As the night wore on, he decided that the only person he respected was our Egyptian comrade--the only Arab in the bar!</P>
<P>Another contradiction: Several years ago there was a brilliant demonstration in New York, where the building trades workers took over the streets illegally! Billy was on that demonstration.</P>
<P>I refused to take off the scarf, and I refused to move. Nothing happened, except that we absorbed his anger to avoid a fight, and for five hours Billy got an earful of what the keffiyah really means.</P>
<P>We told him about the struggle of the Palestinians, but also about how students fighting for free education in Mexico wore the keffiyah, and how Irish people fighting British occupation wore it.</P>
<P>As he left, he told me "You're a good, man, Stymie." (He called everyone Stymie.) "You gotta lotta balls wearing that scarf, but I still think it sucks, and it's MY TOWN!"</P>
<P>"It may be your town," I told him, "but it's MY BAR, so get outta my face." He took off his hat. "My hat is off to you, sir."</P>
<B><P>Brian Jones,</B> New York City</P>

