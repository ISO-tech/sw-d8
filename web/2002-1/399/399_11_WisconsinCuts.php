<P><font face="Times New Roman, Times, serif" size="5"><b>Stop the budget cuts</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mike McHugh, shop steward, AFSCME Local 171</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 22, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MADISON, Wis.--Hundreds of city and county employees, firefighters and local politicians from around Wisconsin rallied at the State Capitol on March 12 to protest Governor Scott McCallum's proposed budget cuts.</P>
<P>McCallum also wants to slash the budget for the University of Wisconsin (UW) and wages for UW workers. The Joint Finance Committee voted to more than double the proposed cuts for the UW system, leading the UW Board of Regents to declare a freeze on new student admissions.</P>
<P>Assembly Republicans also want to cut benefits for state employees and force state institutions to cover the cost of raises for employees. For state workers, whose contract expired last June, this would mean even less than the lousy so-called raises that we were already being offered.</P>
<P>Wisconsin has some of the lowest corporate income and property tax rates in the nation. But rather than force them to pay up, Wisconsin politicians would rather see workers and students bear the burden of budget cuts.</P>
<P>Unfortunately, some union leaders are playing into these divisive tactics. AFSCME Council 24, which represents state employees, has called for a separate rally against the budget on March 28.</P>
<P>State workers should go to this rally to show their disgust with the budget plans. But we also need to realize that it's essential to work with city and county employees, students, community groups and others affected by the proposed cuts to fight for a decent raise for state workers--without tuition hikes and service cuts.</P>
<I><P>A rally against the budget cuts is being planned for May 1 in Madison. Call 608-256-9848 for more information.</P></I>

