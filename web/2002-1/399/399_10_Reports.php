<P><font face="Times New Roman, Times, serif" size="4"><b>Justice for Palestinians</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Derek Wright</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MADISON, Wis.--Nearly 200 people rallied in the bitter cold last week to oppose the Israeli occupation of Palestinian land, to protest U.S. military aid to Israel, and to support Palestinian human rights.</P>
<P>A broad coalition came together for the protest, including Al-Awda and other Palestinian rights groups, Jewish groups against the occupation, the International Socialist Organization, local antiwar coalitions, and others.</P>
<P>During the event, people circulated petitions demanding that UW-Madison divest from companies that do business in Israel, an initiative modeled after the divestment campaign during the movement against apartheid in South Africa.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Stop police brutality</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Brian Jones</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--More than 30 people gathered in Harlem March 6 to protest the police assault on Monique Washington, a longtime community activist. Her crime? Asking a police officer for his badge number.</P>
<P>On February 26, several white undercover cops were making an arrest at 132nd St. and 8th Ave. The suspect was handcuffed and on the ground, but witnesses say the officers were beating him with their radios.</P>
<P>Several people approached and started complaining. Monique, a member of Community School Board 5, left the nearby shop where she works and tried to defuse the situation. But when she turned around to ask for their badge numbers, the officers handcuffed her, tripped her to the ground, maced her and threw her in an unmarked van.</P>
<P>She was placed under arrest and charged with disorderly conduct and resisting arrest! "That's what you get for playing community activist," one cop said as he threw her into the van.</P>
<P>Many who spoke pointed out that the court decision to free the cops who tortured Haitian immigrant Abner Louima at a Brooklyn police station in 1997 had no doubt sent a signal to police that they had a green light to brutalize people.</P>
<P>"It's not enough to complain, you must get actively involved," said Monique, in a special appeal to younger people who were present.</P>
