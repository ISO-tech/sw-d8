<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Why labor's political strategy is failing</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 22, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE AFL-CIO Executive Council last month voted to devote more money to electing "labor-friendly" politicians. So at the New Orleans meeting, AFL-CIO President John Sweeney proposed raising 10.5 cents per union member per month for political campaigns--up from the current 6.5 cents--to support pro-labor candidates in November elections.</P>
<P>According to AFL-CIO political chair and AFSCME President Gerald McEntee, the unions need the increase to match the $35 million to $40 million they've spent on each national election since 1996.</P>
<P>Even so, that's $10 million short of what Sweeney wants for political work. Sweeney justifies this spending by denouncing George W. Bush's "shameless" disregard for working people.</P>
<P>But one has to ask: What will union members get for their money?</P>
<P>In the 2000 election, labor backed Democrat Al Gore. When he was vice president, Gore's "Reinventing Government" program gutted the Occupational Safety and Health Administration while eliminating 377,000 federal jobs. And Gore's 1992 campaign promise to push a ban on striker replacements never materialized in his eight years in office.</P>
<P>What is more, Congressional Democrats' conservative policies have prompted some unions to consider abandoning their blind support for the Democrats.</P>
<P>This could mean translating anger over the Democrats' sellout into real political independence--focusing on organizing new members to build union strength to pressure both political parties, for example, or building an independent labor party.</P>
<P>Instead, the response by some unions has been to support Republicans! "There was a recognition by the AFL-CIO and many unions that the labor movement is too often seen as joined at the hip with the Democratic Party and we need to do things to bring Republicans in," said AFL-CIO political director Steve Rosenthal.</P>
<P>Some union leaders--in the Communications Workers of America, for one--would even like to see funds currently used for new organizing diverted to political campaigns. In fact, the AFL-CIO has already designated about 20 field officers to focus exclusively on politics instead of their usual duties, such as union building and collective bargaining.</P>
<P>Of course, labor's pro-business opportunists like Teamsters President James P. Hoffa have already been wining and dining Republican leaders. Hoffa's support for Bush's plan to drill for oil in Alaska's Arctic National Wildlife Refuge in the name of "creating jobs" won him a star seat at the president's State of the Union address.</P>
<P>But even more traditionally liberal unions are supporting Republicans in the name of political "independence." For example, the Service Employees International Union is supporting Republican George Pataki in the New York governor's race because he pushed through new funding for health care. But since taking office in 1995, Pataki has pursued anti-worker policies--presiding over the reintroduction of the death penalty and tax cuts favoring business and the wealthy.</P>
<P>Meanwhile, a recent <I>New York Times</I> article told the story of a historically loyal Democratic local of the United Steel Workers of America in Ohio that is now considering going Republican to thank Bush for new tariffs on foreign steel.</P>
<P>However, Bush's protectionist steel policy safeguards steel company profits, but doesn't include protection for retiree pensions and medical benefits that steelworkers had demanded.</P>
<P>Labor's political wheeling and dealing between the two major parties amounts to a desperate race to the bottom. And union members will be the ones hurt on the way down.</P>
<P>Instead of using members' money to jockey for a better bartering position at the politicians' table, leaders should use the funds to make the unions themselves more powerful. And that's only possible with a focus on organizing and giving members the support they need--like strike funds--to fight for good contracts.</P>
<P>Only then can unions have the power to challenge attempts by politicians--Republicans and Democrats alike--to make workers pay for the economic crisis.</P>

