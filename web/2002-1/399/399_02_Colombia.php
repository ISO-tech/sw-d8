<P><font face="Times New Roman, Times, serif" size="5"><b>Dropping "drug war" cover in Colombia</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 22, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH White House is planning to ask for the lifting of restrictions that require U.S. funding and military support for the Colombian government to be used solely for "counter-narcotics" operations.</P>
<P>Under the Plan Colombia military aid program kicked into high gear by Bill Clinton, the U.S. government used the smokescreen of a "war on drugs" to back Colombia's war on left-wing rebels.</P>
<P>Human rights groups say that the restrictions were often ignored anyway, with the Colombian military using U.S. money to finance its dirty deals. But now Bush wants to get rid of the cover story. "All we are trying to do is add the words 'counter-terrorism' to what the U.S. can do in helping Colombia," one administration lackey told the <I>Washington Post</I>.</P>
<P>But even the <I>Post</I> had to recognize the truth about Bush's proposal: "[I]t also conforms to a longstanding view of some senior Bush officials--particularly those who worked on Latin America issues in earlier, Cold War administrations--that the United States ought to help Colombia's democratic government fend off a threat from guerrillas who espouse a Marxist ideology."</P>

