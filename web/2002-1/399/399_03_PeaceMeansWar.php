<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Behind the Bush administration's missions to the Middle East</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>When peace means war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 22, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WASHINGTON'S "PEACE" mission to Israel isn't about achieving justice for Palestinians--it's to clear the way for a terrible new U.S. war on Iraq.</P>
<P>Even the U.S. decision last week to push a United Nations resolution calling the Israeli occupation of the West Bank and Gaza "illegal" couldn't disguise the real aim of Gen. Anthony Zinni's meetings with Israeli and Palestinian leaders.</P>
<P>"His visit is being interpreted by some in the Arab world as little more than a public relations exercise, designed to placate America's allies in the region who are unhappy about what they regard as U.S. indifference to Palestinian grievances," the BBC reported.</P>
<P>In fact, Dick Cheney--who met up with Zinni in Israel this week--got some resistance when he toured Middle Eastern countries to lay the groundwork for war on Iraq. That's not because the oil-rich dictators and monarchs that the U.S. considers its Arab allies actually care about justice for Palestinians.</P>
<P>It's because Arab governments are under pressure from millions of people across the Middle East who are well aware of the truth that the U.S. corporate media hides--that Iraq was shattered by the Gulf War a decade ago, and more than 1 million people have died since because of U.S. and UN economic sanctions.</P>
<P>The rulers of countries like Saudi Arabia worry that a U.S. war on Iraq could create a backlash that could threaten their own hold on power.</P>
<P>And the more George W. Bush is determined to use September 11 to justify an attack on Iraq, the clearer it becomes that the U.S. "war against terrorism" is another name for U.S. imperialist aggression.</P>
<P>In fact, while Zinni was talking "peace," Bush visited troops in Fort Bragg, N.C., to demand that Congress make funding of the war "its first order of business." "The best way to secure the homeland is to unleash the mighty United States military," Bush told soldiers.</P>
<P>The fact that Washington's warlords are considering signing on to the Saudi peace plan for the Israeli-Palestinian conflict should expose it for the sham that it is. The proposal would require the U.S. to pressure Israel into abandoning settlements on the West Bank and Gaza--something Washington, which bankrolls and arms Israel, is not about to do.</P>
<P>Even if Israel and Washington agree to the establishment of a Palestinian "state," it won't address the mass expulsion of Palestinians during the creation of Israel in 1948. At best, the Saudi deal would give Palestinians control of scattered bits of the Occupied Territories--just like the fake bantustan "homelands" for Blacks under the racist apartheid regime in South Africa. This very prospect gave rise to the Palestinian uprising, or Intifada, some 18 months ago.</P>
<P>The real aim of the Saudi peace initiative, like Zinni's trip, is public relations--to revive the "peace" process and restore "stability." That is, to keep the whole array of hated and corrupt Middle Eastern regimes in power.</P>
<P>Washington's initiatives for both "peace" and war have the same goal--to ensure that the U.S. maintains its military domination of the world. That's why it's urgent that we expose the fraud of Bush's "peace" plans in the Middle East--and build the opposition to Washington's widening wars.</P>

