<P><font face="Arial, Helvetica, sans-serif" size="2"><b>INSIDE THE SYSTEM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The "Fightin' Whites"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 22, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WHAT DO you do if a school won't drop its racist mascot? When the high school in Eaton, Colo., refused to get rid of a Native American logo for its sports teams--called the "Fightin' Reds"--some American Indian students at the University of Northern Colorado (UNC), located in town, decided to fight back.</P>
<P>They named their intramural basketball team the "Fightin' Whites." Led by Solomon Little Owl, director of Native American Student Services at UNC, the team chose a white man as its mascot.</P>
<P>"The message is, let's do something that will let people see the other side of what it's like to be a mascot," Little Owl said. "I am really offended by this mascot issue, and I hope the people that support the Eaton mascot will get offended by this."</P>
<P>The team is made up of American Indians, Hispanics and whites. Their new jerseys bear the slogan: "Every thang's going to be all white." "It's meant to be humorous," said Ray White, an American Indian on the team. "It puts people in our shoes."</P>
<P>But Eaton School District superintendent John Nuspl said the "Reds" logo isn't derogatory and called the group's criticism "insulting." "Their interpretations are an insult to our patrons and blatantly inaccurate," he whined.</P>
<P>--<I>The (Boulder, Colo.) Daily Camera,</I> March 11, 2002</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Prime real estate for the dead</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>A MASSIVE mausoleum beneath the nearly completed Roman Catholic Cathedral of Our Lady of the Angels may become one of the most prestigious final resting spots in the city. The 1,300 invitation-only crypts will likely start at $50,000 each.</P>
<P>Perpetual residency beneath the Spanish marble floor is to be based on "personal goodness" and "contribution to the community." The head of the archdiocese will decide who fits that description.</P>
<P>Other crypts and 5,000 niches for cremated remains will be made available to supporters of the archdiocese, including those who made substantial donations to the construction of the cathedral.</P>
<P>The archdiocese said it doesn't have a list of "invitees," but prominent donors include media mogul Rupert Murdoch, heiress Betsy Bloomingdale, Roy Disney and Bob Hope.</P>
<P>The suggested $50,000 donation will buy a basic crypt in a high-traffic area. Space in one of six semiprivate chapels or a crypt near the 26 stained-glass windows will cost considerably more.</P>
<P>"Until we have at least a year up and running, we really won't know," said spokesman Tod Tamberg.</P>
<P>--Associated Press, February 10, 2002</P>

