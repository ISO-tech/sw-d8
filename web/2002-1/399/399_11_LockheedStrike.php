<P><font face="Times New Roman, Times, serif" size="5"><b>Strikers digging in at Lockheed Martin</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Tony Kin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 22, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MARIETTA, Ga.--More than 250 strikers and supporters of International Association of Machinists Local 709 gathered for a spirited rally March 15 outside the Lockheed Martin plant here.</P>
<P>Union members--on strike for the first time in 25 years--are demanding more job security and increased retirement benefits from the aerospace giant that just last year won a $200 billion defense contract, the largest in history.</P>
<P>The rally was organized by the Atlanta Labor Council and featured local political, labor and religious leaders, as well as representatives from all the major unions in the metropolitan area.</P>
<P>Addressing the strikers--many of whom carried signs saying, "Save Our Jobs"--Local 709 President Jim Carroll vowed to, "stay the course until we get justice." Shop steward Cassandra Head told <I>Socialist Worker</I>, "They [Lockheed] won't go up on retirement benefits or change the contract language, while the corporate heads give themselves big, million-dollar bonuses."</P>
<P>A strike at a defense plant in the middle of a war is not something you would expect in Marietta, which was once represented in Congress by Newt Gingrich. But as one assembly line worker noted, Local 709 had been anticipating this showdown and began to prepare for a possible strike several months ago by saving money and taking care of bills.</P>
<P>The fact that the U.S. is at war has not softened their determination to stand up for their jobs and pensions. Since the 1980s, when 20,000 worked at the plant, Lockheed has cut its workforce down to 7,000 through outsourcing. This trend would have continued had the latest company offer been adopted.</P>
<P>Echoing this feeling of resistance, Georgia AFL-CIO President Richard Ray got a thunderous cheer when he told the crowd, "We're not scared to take on Lockheed!"</P>
<P>With many Lockheed workers approaching retirement age a lot is at stake. Public support is also evident in a recent poll in the <I>Atlanta Journal-Constitution</I> that showed that 67 percent believe the "strikers' demands are reasonable."</P>
<P>As the rally came to a close, Rev. James Orange declared that "workers will last one day longer." We need to demonstrate our solidarity and support.</P>

