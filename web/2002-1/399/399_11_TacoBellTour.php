<P><font face="Times New Roman, Times, serif" size="5"><b>Taco Bell Truth Tour</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Thomas Ventura and Sarah Knopp</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 22, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IRVINE, Calif.--"Taco Bell take a stand, Living wages we demand!" was the chant as 500 protesters marched on Taco Bell headquarters here March 11 to mark the end of the Taco Bell Truth Tour.</P>
<P>Farmworkers from Immokalee, Fla., led the six-mile march through the streets of Santa Ana and into Irvine, with puppets, banners and signs adorning the demonstration route. Some of the 500 protesters had followed the caravan across the country in a tour that highlighted the low wages and terrible conditions faced by the immigrant workers employed by Six Ls, the grower that supplies tomatoes to Taco Bell owner Tricon Global Restaurants.</P>
<P>Day laborers, SEIU members, and members of immigrant workers' organizations participated in the demo at Tricon's HQ. Romeo Ramirez, an Immokalee farmworker from Guatemala, said that workers have had "a lot of success throughout the campaign." Like other Immokalee workers, he is paid just $7,500 per year, and pays $50 per month to share a trailer with 10 to 12 others.</P>
<P>Tricon tried to claim that the farmworkers had nothing to do with them. But after the marched, Taco Bell officials had to meet with the workers and encourage Six Ls to settle the dispute.</P>
<P>The Truth Tour ended on a high note, with a day of political workshops and discussion preceding the march. Workshops included everything from a presentation by members of the MST landless movement of Brazil to workshops on organizing for global justice since September 11.</P>
<P>And the Immokalee workers' Truth Tour helped to lead the way in recapturing the spirit of protest for justice.</P>

