<font face="Times New Roman, Times, serif" size="5"><b>Punishing the world's poor</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 22, 2002 | Page 5</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH will join other world leaders in Monterrey, Mexico, this week for a United Nations-sponsored conference on world poverty. There, the most powerful men and women on earth will talk about their commitment to ending the suffering faced by the one in six people in the world who survive on less than $1 a day. But ELIZABETH SCHULTE shows why their rhetoric is a lie--and how the system is rigged against the poor.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<P>ALMOST 11 million children die every year from preventable and treatable causes. That was the horrific finding announced last week by the World Health Organization and the United Nations Children's Fund (UNICEF).</P>
<P>Pneumonia, malaria and HIV/ AIDS are big killers. But so is diarrhea, a "disease" directly related to malnutrition. In fact, not getting enough food accounts for 60 percent of all children's deaths, according to the two groups.</P>
<P>This misery is entirely preventable--and with very little effort on the part of wealthy countries like the U.S. "The resources needed to reach every child and adolescent are well within the means of our wealthy world," said Carol Bellamy, executive director of UNICEF.</P>
<P>This is the backdrop to this week's UN Conference on Financing for Development. Representatives of the World Bank, International Monetary Fund (IMF) and World Trade Organization will join 54 heads of government and 300 finance, trade and foreign ministers at the summit. They have a lot to answer for.</P>
<P>The UN has set a modest target for development aid from wealthy countries--just 0.7 percent of their gross domestic products (GDPs). But few industrialized countries are even close.</P>
<P>The U.S. government is dead last on the list, currently contributing only about 0.1 percent of GDP in aid. No wonder U.S. officials have already demanded that any mention of the UN target be removed from the summit's draft declaration.</P>
<P>To pave the way for his appearance in Monterrey, George W. Bush said in a speech last week that the U.S. would contribute up to $5 billion in aid over the next three years.</P>
<P>Bush was applauded for this announcement. "There could be no more potent spokesman for increased aid than George W. Bush, the war leader," said Mark Malloch Brown, administrator of the UN Development Program. But Bush's "generosity" would only increase U.S. aid to 0.12 percent of GDP.</P>
<P>With rock star Bono by his side, Bush also floated the idea that wealthy countries should give outright grants to poor countries--instead of the loans that have saddled the Third World with a massive debt burden. "Many have rallied to the idea of dropping the debt," Bush said. "I say, let's rally to the idea of stopping the debt."</P>
<P>Of course, that would leave poor countries under the bankers' thumbs for what they currently owe. And there are strings attached, too. "Greater contributions from developed nations must be linked to greater responsibility from developing nations," Bush said.</P>
<P>Meaning that U.S. aid to poor countries would be linked not only to implementation of free-market economic policies demanded by the IMF, but to their commitment to the "war against terrorism." In other words, do as we say or starve.</P>
<P>These are the sick priorities that the U.S. government--and its friends at the IMF and World Bank--will bring to the table in Mexico this week. We have to build an opposition to their global economic policies that punish the world's poor--and link it to the fight against Washington's bloody war drive.</P>

