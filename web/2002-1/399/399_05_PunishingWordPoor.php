<font face="Times New Roman, Times, serif" size="3"><P>A CURE that's worse than the disease. This would be a good way to describe the free-market fixes demanded in poor countries around the world by the IMF.</P>
<P>The best example of this today is Argentina, the IMF's star pupil throughout the 1990s. In return for $132 billion in loans over the last decade, the government agreed to implement an IMF "structural adjustment program"--which people around the world now know is another way of saying harsh attacks on workers and the poor.</P>
<P>The IMF demanded tax increases, privatization of state-run enterprises and cuts in government spending on workers' salaries and poverty programs.</P>
<P>Even when Argentina began sinking into crisis in the late 1990s, the IMF demanded more of the same--which only caused the downward spiral to grow worse. Today, half of the population lives in poverty--in what was previously one of the most developed economies in Latin America.</P>
<P>And the global loan sharks aren't even done yet. IMF representatives visited Argentina last week to discuss releasing nearly $10 billion in aid that was frozen after the country defaulted on its payments.</P>
<P>To woo the IMF, President Eduardo Duhalde's government slashed another 15 percent from the government budget. But IMF representative Anoop Singh said he still wasn't satisfied--and left Argentina without guaranteeing any more aid. He did, however, leave behind a 10-point list of demands for new austerity measures.</P>
<P>Anger about these conditions led to a massive revolt last December that forced two presidents to resign in one week's time.</P>
<P>The desire for change hasn't disappeared. Last week, thousands joined demonstrations against the cuts imposed to coax aid out of the IMF, and on Thursday, teachers across the country struck to demand back pay. "We want Duhalde to go," said a protester who was pelting the president's home with vegetables.</P>
<P>At the Washington protests against the IMF and World Bank meetings in April, we have to send a message: Shut down these global loan sharks!</P>

