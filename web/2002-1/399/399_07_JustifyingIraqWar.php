<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Looking for excuses for new war on Iraq</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 22, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"WE ARE at war to keep the peace," explained George W. Bush at a press conference on March 13, paying an unwitting tribute to George Orwell's <I>1984</I>.</P>
<P>Without a trace of irony, the president also warned that he would use nuclear weapons against enemies who "threaten the U.S." or use "weapons of mass destruction against us or our allies or friends."</P>
<P>The Bush administration no longer even feels the need to claim any link between its unfolding war and the quest for "justice" for the victims of September 11. When asked about the whereabouts of Osama bin Laden, who Bush vowed to "smoke out of his cave" only six months ago, the president replied, "I truly am not that concerned about him."</P>
<P>Osama bin Laden was yesterday's "evildoer," used to justify the war in Afghanistan. Now Bush has set his sights on an old demon, Saddam Hussein, to justify the forthcoming attack on Iraq.</P>
<P>Unable to establish any link between September 11 and Iraq, the Bush administration has resurrected its rallying cry against Saddam, that he used chemical weapons "against his own people"--while failing to mention that the U.S. enthusiastically supported him in the 1980s, when he did so.</P>
<P>The U.S. has floated a plan to stage a showdown with Iraq over United Nations (UN) weapons inspectors--who were pulled out before its Desert Fox bombing of Iraq in December 1998--as an excuse to go to war against Iraq once again.</P>
<P>Although Iraq has already indicated its willingness to cooperate with UN weapons inspectors, a U.S. intelligence official said the White House "will not take yes for an answer," according to the London <I>Guardian</I>.</P>
<P>The U.S. will demand unlimited access for the inspectors, conditions designed to force an Iraqi refusal and set the stage for a new war next fall--this time to remove Saddam Hussein from power.</P>
<P>Vice President Dick Cheney emerged from his undisclosed location last week to tour the Arab regimes surrounding Iraq, while firming up plans to launch the attack, which will involve at least 200,000 U.S. troops.</P>
<P>Cheney got no immediate support from Arab rulers, whose own corrupt regimes face the growing possibility of mass rebellion from below. As journalist Robert Fisk argued, "For if the Iraqis were helped to overthrow their dictatorial government, what if Egyptian or Saudi citizens decided on a little 'regime change' of their own?"</P>
<P>The prospect of another U.S. war against Iraq is massively unpopular throughout the Arab world, where it is well known that U.S.-sponsored sanctions, in place for more than a decade, are responsible for the deaths of more than a million Iraqis, half of them small children.</P>
<P>Anti-U.S. sentiment has only grown since September 11, as the Bush administration gave the green light for Israel to use the war on terrorism as its excuse to launch a bloodbath against Palestinians in the Occupied Territories.</P>
<P>But the U.S.--less concerned now with the kind of "coalition building" that gave a cloak of international legitimacy to its war on Afghanistan last fall--is prepared to "go it alone" against Iraq if necessary.</P>
<P>Cheney is "not going to beg for support" from Arab regimes, a senior Bush administration official told the <I>Philadelphia Inquirer</I> before Cheney's Middle East trip. "He's going to inform them that the president's decision has been made and will be carried out, and if they want some input into how and when it's carried out, now is the time for them to speak up."</P>
<P>Through its war on Afghanistan, the U.S. established permanent military bases in Afghanistan, Pakistan, Kyrgyzstan, Uzbekistan and Tajikistan, with facilities in Kazakhstan--corresponding exactly to the route of a planned American oil pipeline from the Caspian Basin. The overthrow of Saddam Hussein will give the U.S. control over Iraq's vast oil reserves, second only to those of Saudi Arabia.</P>
<P>To achieve these strategic aims, the U.S. killed thousands of Afghans and more than a million Iraqis. The privilege of deploying weapons of mass destruction apparently belongs to the United States alone.</P>

