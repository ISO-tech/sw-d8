<P><font face="Times New Roman, Times, serif" size="4">How could they convict Andrea Yates?</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Cruel verdict in Texas</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 22, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MAYBE THE jurors thought that they were making a just "compromise" when they condemned Andrea Yates to life in prison instead of death. But the guilty verdict against Yates in Houston last week was an appalling injustice.</P>
<P>The jury rejected the argument that Yates should be found not guilty by reason of insanity for drowning her five children in the bathtub last June. When police responded to her 911 call, Yates explained that she had killed her children to save them from Satan because she was a "bad mother." The 37-year-old had attempted suicide twice, once telling a psychologist, "I had a fear I would hurt somebody. I thought it better to end my own life and prevent it."</P>
<P>"She would rank among the five sickest--and most difficult to get out of psychosis--people that I've ever treated," said Dr. Eileen Starbranch, a psychiatrist who treated Yates. Yet after three weeks of testimony from 38 witnesses, the jury took only three-and-a-half hours to find Yates guilty of murder.</P>
<P>In fact, they were following the Texas state government's idea of "justice." Under state law, defendants can't be found not guilty by reason of insanity if prosecutors can establish that they knew what they did was wrong.</P>
<P>In the Yates case, prosecutors devoted hours to "recreating" the children's drownings, one by one, with baby dolls. "The cruelest thing I have ever personally witnessed in my life was Andrea being forced to listen to the closing arguments of the prosecutors," said Andrea's husband Russell. "I stopped trying to count the lies."</P>
<P>But the most effective piece of evidence proved to be Yates' 911 call. This--along with a poster defining Texas rules for insanity pleas--were the only pieces of evidence that jurors requested to see during their deliberations. They concluded that, by the letter of Texas law, Yates knew what she was doing and should be punished.</P>
<P>"In an institution, she would be seen as having an illness and would be treated 24 hours a day for an illness," said Sylvia Castillo, director of the parents' assistance group de Madres a Madres. "In jail, she'll be with criminals who are not sick and will try to take advantage of her."</P>
<P>After the trial, Russell Yates--finally able to speak out after Judge Belinda Hill's gag order on witnesses was lifted--said that he didn't blame jurors. "It isn't that they didn't believe she was mentally ill, it's the way the law is worded," Yates said. "She's a victim here, not only of the medical community, but the justice system."</P>

