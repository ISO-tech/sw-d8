<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 22, 2002 | Page 11</font></p>

<P><font face="Times New Roman, Times, serif" size="4"><b>Immigrant airport screeners</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eduardo Capulong</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SAN FRANCISCO--About 50 protesters turned out at Secretary of Transportation Norman Mineta's Commonwealth Club speech at the Renaissance Parc 55 Hotel here March 15, to protest anti-immigrant aviation security legislation. The laws require all airport baggage screeners to be U.S. citizens by November 19.</P>
<P>A delegation tried to deliver to Mineta petitions signed by more than 3,000 people seeking repeal of the requirement. But hotel security and the police stopped them, threatening arrest.</P>
<P>"I was going to tell him: We screeners have nothing to do with 9/11 and are not to blame," Erlinda Valencia, a screener who's been working at the San Francisco airport SFO for 14 years, told <I>Socialist Worker</I>. "It's unfair to us to be laid off for something that's not our fault."</P>
<P>"The only way we're going to make the federal government stop this requirement is to paralyze the airports," said Daz Lamparas of the Asian Pacific American Labor Alliance.</P>
<P>The Immigrant Airport Workers Solidarity Committee is coordinating support for the screeners. The next focus is the Labor for Peace in Justice rally set for Oakland on March 23 and a meeting in Daly City the following day.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>UMass Amherst</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By John Buttell</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AMHERST, Mass.--Resident assistants (RAs) at the University of Massachusetts-Amherst voted March 5 to unionize, becoming the first unit of undergraduate employees in the country to unionize.</P>
<P>The vote comes after two years of fighting a hostile university administration for the right to organize, a fight that went all the way to the state labor relations board. Unless they want to legally challenge the results, the university must now begin contract negotiations with RAs, who voted 138 to 88 to join United Auto Workers Local 2322.</P>
<P>The win follows last semester's vote by instructors in the School of Continuing Education to join the Graduate Employees' Organization (GEO), a unit of Local 2322 that also represents teaching and research assistants.</P>
<P>While the administration has played hardball with campus workers, it has enthusiastically gone along with the budget cuts--announcing plans for the elimination of the child care center and campus security services along with sweeping cuts in academic programs.</P>
<P>The RAs and Continuing Education instructors have shown that it's possible to fight back and win. It's time for students and other campus workers to follow their example.</P>

