<P><font face="Times New Roman, Times, serif" size="4">Huge protests in Barcelona tell EU leaders:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>No to a bosses' Europe</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 22, 2002 | Page 5</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>CRAIG JOHNSON reports from Barcelona, Spain, on the huge protests against the European Union (EU) summit.</P>
</B><P>WITH THE familiar cry "Another world is possible," people from across Europe gathered in Barcelona last week to demand a world for people and not profit. At least half a million demonstrators turned out to protest a meeting of leaders of the European Union.</P>
<P>Safely barricaded behind chain-link fences, armored cars and huge numbers of riot cops, the politicians discussed how to convert Europe into an economic force to compete with the U.S. and Japan.</P>
<P>But outside, activists sent a different message, with days of debates, forums, video showings, concerts and other cultural events, culminating in huge protests. Everywhere, their main slogan could be seen: "Against a Europe of capital."</P>
<P>Importantly, activists also focused attention on key issues like the U.S. war drive and the Palestinian struggle.</P>
<P>The week was capped by two big protests. On Thursday, a march organized by the Confederation of European Trade Unions drew some 100,000 people, representing unions across the EU. Then on Saturday, a reported 450,000 people came out for a final demonstration that united calls against war, imperialism and a Europe for the bosses.</P>
<P>Residents of the city showed their support by cheering from their balconies--and beating pots and pans in the style of the Argentine demonstrations, called "cacerolazos."</P>
<P>As usual, the mainstream media focused on a confrontation between anarchists and police during the big Saturday march. It was police who were on the offensive, attacking the march from the rear and turning the Ramblas, Barcelona's famous main thoroughfare, into a war zone.</P>
<P>The Barcelona protests show that the movement for global justice in Europe remains alive and growing--an encouraging sign that we can, as the slogan from the week put it, "Globalize the Argentinazo!"</P>

