<font face="Times New Roman, Times, serif" size="5"><b>Crooks sue over stolen election</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 31, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>FOR THE Bush administration, "too little, too late" is just enough. Fully 18 months after George W. Bush "won" the 2000 presidential election by stealing the vote in Florida, his Justice Department is finally getting around to filing suit against three Florida counties for voting rights violations.</P>
<P>Federal prosecutors are charging that Miami-Dade, Osceola and Orange Counties failed to provide adequate language assistance to voters with limited English proficiency. Osceola County faces the most serious charges--that some poll workers demanded green cards from Latino voters and told Spanish-speaking voters that they had to speak English to vote.</P>
<P>The suit is a thinly veiled attempt by Bush officials to reach out to immigrant voters as Dubya's baby brother, Florida Gov. Jeb Bush, kicks his re-election campaign into high gear. But the Justice Department has made absolutely no mention of the thousands of Blacks who were illegally purged from voter rolls by Florida Secretary of State Katherine Harris.</P>
<P>"This is a half-hearted attempt to whitewash the department's lack of action, and to sweep aside the pain and humiliation so many voters felt on Election Day," said Al Gore's campaign manager Donna Brazile. But the Democrats are hardly ones to complain.</P>
<P>In the postelection struggle over Florida, Gore refused to raise Black voter disenfranchisement and Harris' racist purge of the rolls. Instead, Gore's recount strategy focused on pregnant, dimpled and pimpled chads--which ultimately cost him the White House.</P>

