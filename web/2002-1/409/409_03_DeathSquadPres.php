<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Colombia's death squad president</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 31, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE CANDIDATE of the death squads--and the U.S government--will be the next president of Colombia. As <I>Socialist Worker</I> went to press, Alvaro Uribe had claimed outright victory in the first round of Colombia's presidential election, making a second round unnecessary.</P>
<P>Uribe has the backing of the United Self-defense Forces of Colombia (AUC)--the country's largest paramilitary group--and its cohorts in the Colombian military. That's terrible news for unionists, left-wing activists and working people throughout the country who have suffered in Colombia's 38-year civil war.</P>
<P>Uribe has an appalling record as a right-winger. In the 1980s, he served as the mayor of the city of Medellin and later as governor of Antioquia--the center of Colombia's cocaine trade. During his time as governor, Uribe helped create a total of 69 "civilian defense groups"--providing them with radios and motorcycles and allowing them to carry weapons. At least two of these groups were found to have provided intelligence to death squads--and at least one may have carried out assassinations.</P>
<P>But the U.S. government didn't even pause over this record. Even before Uribe was declared the winner, U.S. Ambassador Anne Patterson went to his hotel to congratulate him. That's because Uribe's promise to widen the war against the Revolutionary Armed Forces of Colombia (FARC) fits nicely with the Bush gang's aims in Colombia.</P>
<P>"After Mexico and Venezuela, Colombia is the most important oil country in the region," Patterson told the Bogot&aacute; daily newspaper <I>El Tiempo</I>. "After what happened on September 11, the traditional oil sources for the United States are less secure&#133;Colombia has great potential for exporting more oil to the United States, and now more than ever, it is important for us to diversify our oil sources."</P>
<P>Uribe's appeal was his claim to be able to overcome Colombia's economic and social crisis. With paramilitary groups on the rampage and unemployment running at 20 percent, the vote for Uribe was a vote of desperation and economic frustration. Even so, less than half of the country's registered voters cast ballots.</P>
<P>What's more, Uribe's warmongering was legitimized by his predecessor, Andr&eacute;s Pastrana. While Pastrana was known for granting an autonomous zone to the FARC, he used negotiations to gain time for a military buildup, funded by $1.3 billion in U.S. aid for the "war on drugs." In February, Pastrana renewed military attacks on the FARC.</P>
<P>But Uribe's plans for war go much further. He wants to double the size of the Colombian Army and National Police force--and create a force of up to 1 million "civil informants."</P>
<P>Where will the estimated $4 billion cost of this program come from? A good bet would be the U.S. government.</P>
<P>Congress is currently considering a Pentagon spending authorization bill that would remove the limit on how many U.S. military personnel can be deployed in Colombia. Another piece of legislation would justify U.S. intervention in Colombia as part of the "war on terror"--freeing up even more U.S. aid to be sent to Colombia's new president.</P>
<P>U.S. intervention in Colombia will only cause more bloodshed and misery--and the Bush administration knows it. But Washington is perfectly willing to back dictators and butchers around the world as long as it suits their interests.</P>

