<P><font face="Times New Roman, Times, serif" size="5"><b>A liberal gloss for welfare "reform"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 31, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>HILLARY RODHAM Clinton once said, "It takes a village to raise a child." But if the New York senator gets her way with federal welfare "reform" legislation, the village and the child could starve to death.</P>
<P>The welfare law that Bill Clinton signed in 1996 is set to expire this year. Clinton's law set punitive time limits and forced recipients to meet work requirements to get benefits. Now, George W. Bush has more misery in mind. His proposal would increase the work requirements for recipients from 30 to 40 hours a week.</P>
<P>In comes Hillary Rodham Clinton to the "rescue." This supposed liberal is backing a proposal that would require welfare recipients to work 37 hours a week. What a charlatan!</P>
<P>The bill's sponsors are conservative Democratic Sens. Evan Bayh of Indiana and Thomas Carper of Delaware. By endorsing their proposal, Clinton is giving a liberal gloss to this outrageous attack.</P>
<P>"It's a pretty stunning development, and it's had an outsized impact on the debate nationally because of who she is," Deepak Bhargava, executive director of the National Campaign for Jobs and Income Support, told the <I>New York Times</I> at a protest last week in front of Clinton's New York home. "She is perceived as a progressive Democrat, and so she is giving cover to other Democrats to do the wrong thing."</P>
<P>Bertha Lewis, New York executive director for the group Acorn, pointed out that even right-wing Sen. Orrin Hatch (R-Utah) supports keeping the work requirement at 30 hours.</P>
<P>Clinton says that she agreed to sign on to the proposal after a provision for $8 billion in child-care funding was added on. But Republicans had already admitted that they would give a little on this issue--and Clinton is only asking for a very little.</P>
<P>The protesters were right to speak out last week. We have take on these politicians--Republicans and Democrats alike--who want to slash away at desperately needed programs for the most vulnerable.</P>

