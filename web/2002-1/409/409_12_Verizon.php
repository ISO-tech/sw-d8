<P><font face="Times New Roman, Times, serif" size="4">Verizon breaks contract to ram through layoffs</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>We'll fight for our jobs</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By members of CWA Locals 1106 and 1109</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 31, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THOUSANDS OF union telephone workers in New York City will turn out May 29 for a rally against layoffs at industry giant Verizon.</P>
<P>Claiming that the economy is squeezing the company, management declared that 1,718 jobs are "surplus"--with 682 to be immediately eliminated. These would be the first layoffs in the history of the company and its predecessor in New York.</P>
<P>In response, Communications Workers of America (CWA) set a deadline of May 31 for management to agree to arbitration--or we strike. </P>
<P>The mass rally at Verizon headquarters May 29 will give us momentum for the fight ahead. Verizon is crying poverty as a cover for the layoffs. But it's still one of the strongest players in the field.</P>
<P>Verizon Wireless leads the industry in profitability, winning 186,000 new customers last year. Long-distance business grew by 60 percent over the last year to 8.2 million customers, and DSL lines jumped by 88 percent to 1.4 million lines. </P>
<P>For Wall Street and management, the weakening economy is their opportunity to crack the union. Verizon Chief Financial Officer Fred Salerno told analysts that cost-cutting will be "institutionalized." Already, the company has reduced overtime to a bare minimum--cutting the equivalent of 12,000 jobs.</P>
<P>The pressure to attack labor runs throughout the industry. Telecommunications companies were hammered by the burst of the high-tech bubble. Competition for wireless service has taken a toll on companies that provide land lines, and the industry as a whole is saddled with enormous debt from the building spree of the 1990s.</P>
<P>SBC, which owns Pacific Bell, Ameritech and Southwestern Bell, has announced 1,600 layoffs if unions accept a 32-hour workweek--but more than 5,000 layoffs if they refuse. Several thousand more union jobs in the industry have been eliminated through early retirement packages.</P>
<P>For its part, Verizon has been violating our contract ever since we won our strike in 2000. After accepting a neutrality agreement for unionizing the wireless sector, management went to every length to block our union from organizing. </P>
<P>The company also imposed the Service Excellence Program (SEP)--better known to some as the "suspend everyone program"--which links discipline to productivity and can lead to suspension and firing. </P>
<P>After 10 years of industry expansion and good contracts, the CWA leadership was unprepared for this onslaught. Our winning strikes in 1998 and 2000 were brief compared to the six-month strike in 1971 and a four-month walkout in 1989.  But the threat to the union is even greater today and will require a militant stance to beat it back. </P>
<P>CWA leaders have been right to mobilize our union and call for:</P>
<P>--Working safely and by the book;<BR>
--Publicity campaigns to expose the terrible state of telephone facilities;<BR>
--Filing grievances over every contract violation;<BR>
--The biggest presence possible at the rally;<BR>
--An unfair labor practices strike to force arbitration of layoffs.</P>
<P>It's hard to predict where this fight will go. Verizon may agree to arbitration--or provoke a walkout and immediately seek a court injunction to force us back to work. In either case, the fight isn't over.</P>
<P>The push for layoffs won't go away--this is merely the opening round in a fight that will last through the end of the contract in 2003, when we will need to be ready to walk again.</P>
<P>The company has the law and the government on its side. Relying on arbitrators, the National Labor Relations Board and injunctions could seal our fate before we even begin to fight. We need to remember that CWA in New York stands in a long tradition of militant struggle.</P>
<P>Before the 1971 contract, the lack of a no-strike clause meant that our union could flex its muscles regularly. The CWA walked out and even threatened building occupations. These practices laid the basis for beating back the company in 1971.</P>
<P>Members still have the power to defeat the company, but only if we mobilize to the fullest. We need to rebuild rank-and-file power at every work site--to take on Verizon now and in 2003.</P>

