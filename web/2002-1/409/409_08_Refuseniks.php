<P><font face="Times New Roman, Times, serif" size="5"><b>Limitations of the refuseniks</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 31, 2002 | Page 8</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>Mark Cooper recently wrote about the "sobering" speeches given by Israeli "refusenik" soldiers on tour in America speaking against Israel's occupation (<I>SW,</I> May 24). He notes that refusenik Guy Grossman's speech made no mention of the refugees of 1948. This is a fundamental problem for refuseniks. </P>
<P>While we can--and must--use their voices to raise awareness about the Middle East conflict, we must also clearly recognize their limitations. Many proudly serve in other capacities with the Israel Defense Forces. </P>
<P>The refuseniks who spoke recently at my school want a Jewish-only state. While they spoke to a packed room and raised awareness about the occupation, they also misled the audience about U.S. aid to Israel.</P>
<P>Endorsing the event would have sent a mixed message. Truly anti-Zionist Jews do exist, and they speak out: Eduardo Cohen, Susan Greene and Hagit Borer, to name a few. </P>
<P>The refuseniks are only a means to an end, not an end in themselves.</P>
<B><P>John Green,</B> Davis, Calif.</P>

