<P><font face="Times New Roman, Times, serif" size="5"><b>Do the Teamsters have a winning strategy?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 31, 2002 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"WE MUST remember that all of our struggles are interrelated, whether we are on the picket line at Pepsi in Kansas City, or enjoying the fruits of a successful strike and subsequent contract victory at Coca-Cola in Westboro, Massachusetts," Teamsters President James Hoffa wrote one year ago. "As Dr. Martin Luther King Jr. said, 'Whatever affects one directly, affects all indirectly.'"</P>
<P>Fine words. But soon after Hoffa wrote them, PepsiAmerica began replacing striking Teamsters in Kansas City--and the union swept the defeat under the rug.</P>
<P>Now the company is coming after the union again--this time in Chicago, a Hoffa stronghold. But Hoffa did nothing to prepare for this fight--not even after the company's rival, Pepsi Bottling Group, provoked strikes.</P>
<P>If the Pepsi bottlers are taking aim at the Teamsters, its because Hoffa has shown himself to be a paper tiger. After calling a strike for a union contract at Overnite Transportation in late 1999, he soon showed that he had no strategy to counter the company's strikebreaking. Today, the strike continues in name only.</P>
<P>Hoffa's do-nothing leadership doesn't mean that Teamster strikes can't win. A two-month Teamster strike last year in New Jersey forced the Pepsi Bottling Group to drop its harshest demands.</P>
<P>Soon afterward, in a 12-week strike against Pepsi Bottling Group in Minnesota, the 440 members of Teamsters Local 792--which voted for reformer Tom Leedham over Hoffa in the last two union elections--mobilized to win.</P>
<P>They picketed Pepsi stands at the state fair, held rallies with other unions and set up ambulatory pickets wherever scab drivers delivered their goods. Ultimately, the company had to significantly improve the wages and benefits proposed in its "best and final offer."</P>
<P>But since the recession, Teamster employers like UPS are taking a much more hard-line approach.</P>
<P>Rank-and-file Teamsters can't wait for Hoffa and his allies. Union members are going to have to take the initiative and develop an aggressive strategy that can take on the employers and win.</P>

