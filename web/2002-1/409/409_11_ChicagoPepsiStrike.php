<P><font face="Times New Roman, Times, serif" size="4">Union-busting bottling company attacks Teamsters</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Pepsi drivers strike in Chicago</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 31, 2002 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--More than 300 drivers and delivery workers hit the picket lines at three PepsiAmericas plants on May 21.</P>
<P>Workers, members of Teamsters Local 744, walked out over company demands for a five-year contract that would eliminate delivery workers' commissions on sales and force workers to pay 15 percent of health insurance costs.</P>
<P>Tragically, production workers--also members of Local 744--voted to accept a separate contract and are crossing picket lines under a no-strike deal.</P>
<P>But drivers and delivery workers are determined to draw the line. While workers were offered a 30-cent per hour increase, the elimination of commissions would reduce drivers' and helpers' paychecks from $50,000 to $60,000 per year to about $35,000 to $40,000.</P>
<P>What's more, the proposed deal would allow PepsiAmericas to increase employee health insurance payments by as much as 12 percent every year, according to Local 744 Vice President Tom Kapp.</P>
<P>"This company has no respect for workers," said Dennis, a striker. "You have guys working here 20, 27 years, and the company wants to take away their health care."</P>
<P>Another striker, George, has worked at the company for 20 years, 17 in the Pepsi Express department. "We were selling 250,000 cases then. We are selling 600,000 cases now. But 17 years later, I have a $1.50 an hour raise," to $14 an hour.</P>
<P>Anthony Pick, a utility driver, said the company's proposed lump-sum payments to pay for health care increases are meaningless. "It's just like the government did with taxes--they are giving our own money back to us. And who knows how much we're going to pay in insurance by the time this contract is over? I'm out here because I love my wife and family."</P>
<P>But the company isn't vicious because it's broke. PepsiAmericas' net income for the first quarter of 2002 was $23.4 million--an 82.8 percent increase over the same period a year earlier.</P>
<P>Nevertheless, the company last year forced Teamsters Local 838 near Kansas City on strike by demanding a freeze on retirement benefits, elimination of sick days, unlimited overtime and the option of decreasing or eliminating health insurance.</P>
<P>When members rejected union leaders' recommendation to settle after six weeks on strike, PepsiAmericas began hiring permanent replacements. That's why Local 744 leaders in Chicago should have seen this fight coming.</P>
<P>On May 11, delivery workers as well as warehouse workers voted to reject a concessions-filled contract. But union leaders sent workers back to their jobs without a contract. This only undermined workers' unity--which is exactly what PepsiAmericas intended.</P>
<P>At the second vote three weeks later, drivers, who voted down the initial offer by 178-32, voted to strike by only 100-81. Warehouse workers, whose pay is unaffected by commissions, voted 102-84 to accept the deal after management threatened to replace them.</P>
<P>The sad result is that Local 744 is officially scabbing on itself. Some drivers even crossed the picket line in the first week of the strike.</P>
<P>The defeat of the Kansas strike last year shows just how far PepsiAmericas is willing to go to break the union. But this strike can still be won. "They haven't broken our spirit," striker Anthony Pick said. "This strike can be won."</P>
<P>Members of Teamsters Local 744 should follow this example--and appeal for solidarity from the warehouse workers in their own local as well as unions around the city. </P>

