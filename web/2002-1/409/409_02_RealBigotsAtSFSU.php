<P><font face="Times New Roman, Times, serif" size="4">Palestinian students at SFSU set record straight</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Exposing the real bigots</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Todd Chretien</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 31, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ISRAEL SUPPORTERS caused a storm of controversy at San Francisco State University (SFSU) by claiming that pro-Palestinian students organized an anti-Semitic counterprotest opposite a May 7 pro-Israel rally. But Palestinian rights activists are speaking out to set the record straight--and to expose the real bigots at SFSU.</P>
<P>Thanks to the frenzied efforts of a pro-Israel professor, the supposed anti-Semitic slurs of Palestinian rights supporters became national news. SFSU President Robert Corrigan added fuel to the fire when he took out a full-page ad in the campus newspaper--and directly e-mailed more than 20,000 students--to denounce pro-Palestinian students who "abandoned themselves to intimidating behavior and statements too hate-filled to repeat."</P>
<P>Corrigan wasn't at the May 7 event and relied on pro-Israel faculty and students for his facts. For weeks after the rally, university officials claimed to have videotaped evidence to prove the charges against the counterdemonstrators. But they refused to release anything.</P>
<P>And when SFSU turned over material to the District Attorney's office for a possible hate crimes investigation, pro-Israel demonstrators were--for the first time--singled out for their racism against Palestinian supporters.</P>
<P>At a May 23 press conference, the General Union of Palestinian Students (GUPS) released videotapes of the rally that refute the charges against their supporters--and show what really happened.</P>
<P>"Pro-Israel supporters began to taunt and tease [Palestinian students]," said Fadi Shamieh, a GUPS officer. "Comments such as 'animals,' 'terrorists,' 'sand niggers,' 'camel jockeys,' were hurled in our direction." As Shamieh concluded, "The events of May 7 fit into a continued pattern of discrimination&#133;President Corrigan, along with others on campus, in their attempts to take away from the Palestinian movement, are attempting to label us anti-Semites and hate mongers. They are sadly mistaken. We stand firmly against anti-Semitism and all other forms of racism. We will not be intimidated by these politically motivated attacks on pro-Palestinian students." </P>
<P>Alicia Thorndike, an SFSU student and member of the International Socialist Organization, also described facing harassment and threats from pro-Israel students. "We won't let Corrigan's false charges stop us," she said. "We demand that all charges be dropped against GUPS and that the university divest all financial holdings from the state of Israel."</P>
<P>Corrigan seemed to retreat after the truth was revealed at the GUPS press conference. He's reportedly set to agree to a deal where all charges will be dropped if pro-Palestinian students agree to meet with pro-Israel students to "diffuse tensions."</P>
<P>Having set the record straight, GUPS and other student activists plan to launch a Students for Justice in Palestine chapter next semester--to demand divestment of all university holdings from companies that profit from Israeli occupation or military aggression.</P>

