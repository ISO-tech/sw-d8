<P><font face="Times New Roman, Times, serif" size="5"><b>Expelled at UC for taking a stand</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Keith Danner</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 31, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>JUST WEEKS after they were forced to reverse their suspension of Students for Justice in Palestine on the Berkeley campus, administrators for the University of California (UC) system are at it again.</P>
<P>Two UC students were expelled from the Education Abroad Program in Cairo, Egypt, which has ties to the statewide university system. The students' offense? Bringing food and water last month to Palestinians besieged by Israeli troops in the Church of the Nativity in Bethlehem.</P>
<P>The two students are members of the International Solidarity Movement (ISM) and were part of a group of activists who broke through Israeli lines to get to the church. When the siege ended, Israeli authorities took the ISM members into custody and were still holding them as <I>Socialist Worker</I> went to press.</P>
<P>Apparently, this act of political commitment was against UC rules. The two students (the father of one asked that we keep his son's name out of print until he is released by Israel and returns to the U.S.) were "in a dangerous situation," university officials said, after having signed a contract not to go "anyplace dangerous" while in the study abroad program. </P>
<P>On May 17, the parents of the two got letters notifying them of the expulsion--and giving 48 hours for an appeal. The fact that the two were both in detention in an Israeli jail apparently made no impression on UC higher-ups. The 48 hours passed, and the students were officially expelled.</P>
<P>Rather than celebrate the bravery of these students, the university has gone out of its way to punish them. But activists at UC-Riverside, where one of the students went to school, are speaking out. On May 22, they held a rally for the expelled students. </P>
<P>We need more actions like this to show UC that they can't get away with this.</P>

