<P><font face="Times New Roman, Times, serif" size="5"><b>Market priorities wreck planet</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 31, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE UNITED Nations' latest report on the environment is the most terrifying yet. The study, for the upcoming World Summit on Sustainable Development, predicts dire consequences as a result of "market forces [that] drive the globe's political, economic and social agenda."</P>
<P>The report predicts that in 30 years, more than 11,000 plant and animal species will be dead or dying. More than 50 percent of the world's population will face moderate to severe water shortages. Coastlines will be clogged with pollution.</P>
<P>It's not as though there aren't laws to protect against this kind of environmental degradation. "We now have hundreds of declarations, agreements, guidelines and legally binding treaties designed to address environmental problems and the threats they pose to wildlife and human health and well being," admits Klaus Toepfer, executive director of the UN Environment Program.</P>
<P>The problem is that it's simply not as profitable for bosses to follow the rules.</P>

