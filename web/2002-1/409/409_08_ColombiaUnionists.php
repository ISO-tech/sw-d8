<P><font face="Times New Roman, Times, serif" size="5"><b>Dangers for union activists inside Colombia</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 31, 2002 | Page 8</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>,</P>
</B><P>In Colombia, trade union activism is a dangerous affair. Orlando Herran, national president of the Colombian Drivers' Union, discussed the violent situation in his country and the targeting of union leaders on April 13 at a teach-in at the University of the District of Columbia.</P>
<P>Herran is one of a group of union leaders currently seeking refuge in the U.S. because of death threats they received in Colombia. Union leaders are routinely harassed and targeted for death simply for engaging in union organizing or publicly criticizing the abuses committed by paramilitary forces, the Colombian government and the army.</P>
<P>Herran knows about the struggle for human rights in Colombia firsthand. On December 21, 2001, a tragedy occurred in the Herran family, when Edgar Herran, Orlando's brother and a legal adviser to the same union, was murdered by paramilitaries. Orlando and the rest of his family were also targeted, but were warned by a phone call from Edgar before he was assassinated. </P>
<P>Orlando was then faced with a choice: continue the struggle for union and human rights (and thereby endanger his life and the lives of his family), or quit and let the paramilitaries and government win.</P>
<P>Herran and his fellow trade unionists continue today to demand a better quality of living for workers and the Colombian people. We should all support their struggle.</P>
<B><P>Maurice Alvarado,</B> Washington, D.C.</P>
