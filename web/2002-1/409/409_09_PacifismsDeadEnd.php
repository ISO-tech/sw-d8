<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Why pacifism is a dead-end strategy</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 31, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>FOR AT least some antiwar and social justice activists, <I>all</I> violence, <I>period,</I> is wrong. This is based at least in part on a healthy revulsion. Violence is, by definition, nasty and brutish.</P>
<P>But a principled opposition to violence, if held fast, comes to inadvertently accept it. The most obvious example, that of using force to prevent a rape (either by the person being raped or a third party), shows this clearly. Refusing to use force to prevent an assault means accepting the assault, for to use force to prevent the assault, according to the pacifist, is to sink to attacker's level.</P>
<P>Transferred to the plain of social relations, this means the violence of the slaveholder and the violence of the slave are both morally wrong. More provocatively, the violence of the Nazis to murder Jews and the violence of the Warsaw Ghetto fighters to resist the gas chamber are morally equivalent. </P>
<P>While the vast bulk of violence in our world is committed upon the oppressed by the oppressor, the pacifist exhorts both to refrain from violence. In practice, this means exhorting the slave to refrain from violence against the slaveholder.</P>
<P>The philosopher John Locke put it quite eloquently: "If the innocent honest man must quietly quit all he has for Peace sake, to him who will lay violent hands upon it, I desire it may be consider'd, what kind of Peace there will be in the World, which consists only in Violence and Rapine; and which is to be maintain'd only for the benefit of Robbers and Oppressors. Who would not think it an admirable Peace betwixt the Mighty and the Mean, when the Lamb, without resistance, yielded his Throat to be torn by the imperious Wolf?"</P>
<P>"But pacifism is not passive," a nonviolent activist might argue. Look at the civil rights movement--the sit-ins and the demonstrations. This is certainly true. In the majority of protests, the armed forces of the state are violent, not the protesters.</P>
<P>Moreover, when people are motivated to organize mass protest to redress grievances, they largely do so with peaceful intentions. When Russian workers marched on Winter Palace in January 1905, they did so with a petition, and carrying icons of their beloved Tzar. Only after 1,000 of them were mowed down by the Tzar's troops were they driven to revolutionary action.</P>
<P>Ultimately, any struggle aimed at compelling the ruling class to do something against its will, however it is dressed up, will involve some form of coercion to be successful. Blockading a WTO meeting to try to prevent the delegates from entering, for example, is a form of coercion, and therefore, of force. "Once the principle of coercion and resistance has been accepted as necessary to the social struggle," writes the religious pacifist Reinhold Niebuhr, "pure pacifism has&#133;been abandoned."</P>
<P>To achieve desegregation in the South, nonviolent tactics were often successful. But even in this case, the appearance is misleading. Many of the nonviolent protests were protected by armed men--the "Deacons of Defense," a gun-toting Black security force that protected Black neighborhoods and civil rights organizers from Klan nightriders.</P>
<P>Rather than being morally debased to the level of their attackers, Blacks who defended their neighborhoods discovered confidence and pride in themselves. But principled nonviolence means rejecting forceful methods even if it means defeat and slaughter.</P>
<P>When Franco rose up in Spain to impose a fascist dictatorship, were workers wrong to take up arms to defend themselves? A principled pacifist "yes" is a bankrupt answer that can only mean: Offer your heads to the executioner.</P>
<P>Marxists therefore make a distinction between the violence of the oppressor and the violence of the oppressed. Tactically speaking, it is not a question of violence versus nonviolence, but what means are best to create a society free of the brutality and war endemic to capitalism.</P>
<P>Our motto is "peacefully if we may, forcefully if we must."</P>

