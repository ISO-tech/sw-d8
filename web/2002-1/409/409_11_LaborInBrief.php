<p><a name="nurses"></a><font face="Times New Roman, Times, serif" size="4"><b>Madison, Wis., nurses</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mike McHugh</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MADISON, Wis.--About 80 nurses and supporters rallied at the State Capitol May 7 to call for a ban on mandatory overtime.</P>
<P>Nurses in Wisconsin--and around the country--are routinely forced to work consecutive shifts because of understaffing. Recent studies in Wisconsin have shown that many Wisconsin nurses are cutting back on hours or leaving nursing altogether due to stress caused by inadequate staffing.</P>
<P>The rally was sponsored by Service Employees International Union Local 1199. "We work with human beings whose lives hang in the balance," said speaker Lisa Stauffacher, a 30-year registered nurse at University of Wisconsin Hospital. "Our patients deserve better than nurses who are tired at the end of a 16-hour shift."</P>
<P>A bill was introduced last year in Wisconsin that would have banned mandatory overtime, but it was never voted on. "The health care industry decided to kill [the bill] with lots of money," said State Representative Dwayne Johnsrud.</P>
<P>Wisconsin State AFL-CIO President David Newby reminded the crowd that five workers were killed in Milwaukee in May 1886 fighting for the eight-hour day. "We're still, 116 years later, fighting for the eight-hour day," Newby said.</P>

