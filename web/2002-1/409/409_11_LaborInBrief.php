<p><a name="Angeles"></a><font face="Times New Roman, Times, serif" size="4"><b>University of California-Los Angeles</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Gillian Russom</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LOS ANGELES--About 150 students rallied on the University of California-Los Angeles (UCLA) central campus May 10 to support food service and custodial workers' efforts to unionize. The 130 mostly Mexican immigrant workers who work at UCLA's student union building (ASUCLA) are fighting to join AFSCME Local 3299.</P>
<P>They have no sick time, vacations or health benefits. And they can't earn seniority, since they are hired in the fall through a temporary agency and then fired at the end of the school year. </P>
<P>Workers and students demanded that ASUCLA's Board of Directors, which is elected by students, support the workers' efforts.</P>
<P>Protesters read aloud workers' testimonies. "My name is Alberto Ramirez. I've worked for ASUCLA for five years, although they call me temporary. When I had to take care of my sick wife I was laid off. It is outrageous that in the year 2002 there are still people treated like slaves." </P>
<P>Protesters marched to the ASUCLA Board of Directors' meeting to demand that they pass a resolution supporting workers. "This is a step that goes to the heart of the fight against neoliberalism," Fernando Gapas&iacute;n, president of AFSCME Local 1108 and a Chicano studies professor, told <I>Socialist Worker</I>. "In 1995, the [UCLA] administration decided it was going to increase its profits on the backs of these workers. In Italy, a whole country of workers is on strike because of things like this." </P>
<P>Jose Sanchez, a union member at the UCLA faculty center, was there in solidarity. "Workers deserve to have a just wage to pay their rents and health care for their children," he said, "With these wages, we just can't."</P>
<P>Board members claimed that only UCLA's president has the power to approve unionization, but they issued a statement of support and conceded that the workers are UCLA employees.</P>
<P>Future solidarity actions are planned to keep the heat on.</P>

