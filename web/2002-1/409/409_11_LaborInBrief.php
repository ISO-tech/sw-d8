<P><font face="Times New Roman, Times, serif" size="4"><b>New York City Labor Against War</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Laura Durkay</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--New York City Labor Against the War (NYCLAW) mobilized about 50 trade union members and supporters May 21 to picket the AFL-CIO's Executive Council meeting with a top Israeli official.</P>
<P>NYCLAW organized the protest on short notice when they learned that members of the Executive Council would be meeting with Israeli Consul General Alon Pinkus at the Sheraton Hotel.</P>
<P>Every year, the AFL-CIO invests a portion of union members's dues in Israeli government bonds. But NYCLAW supporters came out to send a message: Not all union members support Israel's war on the Palestinians. The demonstration called for divestment from Israeli bonds, as well as and end to Israel's occupation and the right of return for Palestinians.</P>
<P> "Israel is carrying out ethnic cleansing," said Marty Goodman, a member of the executive board of TWU Local 100. "We're completely opposed to the labor movement giving any support to Israel's genocidal war."</P>
<P>During the protest, three union members tried to enter the Sheraton Hotel to meet with the Executive Council. When they tried to enter the meeting, they were accused of supporting the bombing of the World Trade Center, then kicked out by security.</P>
<P>Michael Letwin, a leading member of NYCLAW and president of the Association of Legal Aid Attorneys/UAW 2325, explained why it was important for union members to protest Israel's war. "American workers need to be on the side of human rights and support the Palestinians and the Israeli peace movement," Letwin said. "It's bad enough that our tax dollars are going to fund Israel, but our union dues--that's intolerable."</P>

