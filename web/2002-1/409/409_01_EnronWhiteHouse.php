<P><font face="Times New Roman, Times, serif" size="4">Enron and the White House</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Crooked to the top</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 31, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>SINCE LAST December, the Bush administration has claimed to be an innocent bystander in the collapse of energy giant Enron. But documents released by the White House last week show that the Bush gang was best buddies with these corporate crooks.</P>
<P>The records reveal dozens of contacts between administration officials and Enron executives last year--four times more than the White House admitted in January. As the largest corporate contributors by far to George W. Bush's political career, Enron executives enjoyed a place of honor in the new White House.</P>
<P>Even as the company was plummeting toward bankruptcy, these corporate con artists were begging their pals for a bailout. Of course, that was after they had sold more than $1 billion worth of company stock--getting out before the price tanked, while Enron workers saw their retirement savings go up in smoke.</P>
<P>When Enron went into free fall, the Bush administration mounted a full-scale damage-control campaign, coordinated by Bush's inner circle of advisers. There are more revelations to come.</P>
<P>Last week, a federal judge ruled that the government watchdog group Judicial Watch can continue its efforts to obtain records from Dick Cheney's energy task force. This could finally reveal proof of what most people assume--that Enron and the other giants of the energy industry bought access to write the administration's policy for their own benefit.</P>
<P>White House officials insist that Enron's crash is an isolated case of corporate irresponsibility--and has nothing to do with them. But for many people, Enron has come to symbolize everything that's wrong with crony capitalism, American style.</P>
<P>As an editorial in the far-from-radical <I>Washington Post</I> put it: "[The Enron scandal's] many tentacles are united by a sense that rich insiders are rigging the system at the expense of ordinary Americans. Enron's energy dealers ripped off California's consumers--and got away with this behavior because of Enron's clout in Washington."</P>
<P>Enron may have bought influence in Washington more aggressively than other big corporations. But it's hardly an exception. And thanks to their servants in Washington, most of Corporate America's rip-offs are perfectly legal--even if they wreck the lives of working people.</P>
<P>Enron is only the clearest example of a system that serves corporate power--at our expense.</P>

