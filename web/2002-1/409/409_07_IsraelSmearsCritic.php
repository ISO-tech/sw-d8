<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Israel's supporters try to silence critics</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 31, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"I RECENTLY visited the Occupied Territories for the first time. And yes, I'm afraid they can reasonably be described as resembling Bantustans--reminiscent of the ghettoes and controlled camps of misery one knew in South Africa," wrote poet Breyten Breytenbach in a letter to Britain's <I>Guardian</I> newspaper in April.</P>
<P>Breytenbach should know. He was jailed from 1975-82 for his opposition to the apartheid regime under South Africa's Terrorism Act.</P>
<P>At that time, the U.S. also regarded the African National Congress (ANC) to be a "terrorist" organization. Dick Cheney refused to meet Nelson Mandela in jail, calling him a terrorist for leading the struggle against South African apartheid.</P>
<P>Now the same smear tactics are being used to discredit those who speak out against Israeli apartheid. As Breytenbach noted, "It is blatantly averred, again and again, that any criticism of Israel's policies is an expression of anti-Semitism."</P>
<P>The chorus charging Israel's critics of anti-Semitism rises to a fever pitch with each new set of Israeli atrocities against Palestinians. Last month, Israel's Prime Minister Ariel Sharon accused those demanding an investigation of Israel's Jenin war crimes of committing anti-Semitic "blood libel."</P>
<P>House Majority Whip Tom DeLay invoked images of Hitler as he warned those who oppose Israel's oppression of Palestinians that "abandoning Israel&#133;ignores the most profound lessons of Europe's history."</P>
<P>Israel's liberal apologists have joined this right-wing chorus in recent weeks. For example, a May 15 <I>Village Voice</I> article entitled, "Tipping Toward Hate," by Alisa Solomon, accused Berkeley pro-Palestinian activists of "cross[ing] way over the line into anti-Semitism in March: The glass door of the Jewish student center was smashed with a cinderblock. 'F-- Jews' was scrawled on the center's recycling bins."</P>
<P>No evidence exists of any connection between any member of the Berkeley chapter of the Students for Justice in Palestine (SJP) and the anti-Semitic attack--a fact Solomon failed to mention.</P>
<P>She went on to accuse San Francisco State University pro-Palestinian activists of physically threatening a pro-Israel rally on May 7, chanting "Hitler did not finish the job"--accepting as fact the claims of the pro-Israeli protesters. Solomon did not report that those claims have been angrily refuted by the General Union of Palestinian Students (GUPS), which stated plainly, "We stand firmly against anti-Semitism and all forms of racism."</P>
<P>The GUPS students say that the university erected barricades and brought police onto campus to intimidate them, and that "racist slurs were hurled" against Palestinians (for video footage of the altercation visit their Web site at www.sfgups.cjb.net).</P>
<P>GUPS has demanded an apology and a retraction from the university administration, which they argue, "in their attempts to suppress our voices and take away from the Palestinian movement, is attempting to label us anti-Semites and hate-mongers."</P>
<P>There is a rising tide of racism in U.S. society--but it is directed toward, not from, Palestinians and Muslims. Cynical comedian Dennis Miller, made this racist comment about Palestinians on his HBO show: "[L]et's call them what they are: 'Other Arabs Who Can't Accomplish Anything in Life and Would Rather Wrap Themselves in the Seductive Melodrama of Eternal Struggle and Death.' I know that's a bit unwieldy to expect to see on CNN. How about this then: 'Adjacent Jew-Haters.'"</P>
<P>Congressman Dick Armey (R.-Texas), speaking on May 2 on CNBC, advocated ethnic cleansing to solve the crisis in the Middle East, asserting, "I'm content to have Israel grab the entire West Bank&#133;I happen to believe the Palestinians should leave."</P>
<P>In this climate, as Alexander Cockburn argues in the <I>Nation</I> this week, "[T]he left really has nothing to apologize for, but those who accuse it of anti-Semitism certainly do. They're apologists for policies put into practice by racists, ethnic cleansers, and, in Sharon's case, an unquestioned war criminal who should be in the dock for his conduct."</P>
<P>Or, as Breytenbach, put it, "Why should we look the other way when it is Israel committing crimes? A viable state cannot be built on the expulsion of another people who have as much claim to that territory as you have."</P>

