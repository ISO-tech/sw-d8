<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">India and Pakistan go to the brink of an all-out war</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Nuclear nightmare in the making</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 31, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>INDIA AND Pakistan are both U.S. allies in the "war against terror," according to George W. Bush. Now these countries are terrorizing each other--and the world--with the threat of nuclear war. And Washington set the stage for this conflict with its power grab in neighboring Afghanistan and Central Asia.</P>
<P>The U.S. military estimates that a full-scale nuclear exchange between India and Pakistan could kill as many as 12 million people immediately and injure 7 million more. "Even a 'more limited' nuclear war--as measured in number of warheads--would have cataclysmic results, overwhelming hospitals across Asia and requiring vast foreign assistance, particularly from the United States, to battle radioactive contamination, famine and disease," the <I>New York Times</I> reported.</P>
<P>The immediate cause of the crisis was a May 14 attack on an Indian army base in Jammu, which left 30 people dead and several dozen injured. The Indian government claims that the attack was carried out by the same pro-Pakistan groups responsible for last December's assault on the Indian parliament.</P>
<P>But Indian politicians, led by the right-wing Hindu fundamentalist Bharatiya Janata Party (BJP), have been preparing for a war for some time--with the epicenter in the disputed state of Kashmir, which is partitioned between Indian and Pakistani areas of occupation.</P>
<P>The BJP is whipping up war fever to take attention away from recent electoral defeats and anti-Muslim massacres in the state of Gujurat. But if India's Prime Minister Atal Behari Vajpayee thinks he can get away with squeezing Pakistan now, it's because the U.S. has been putting pressuring on Pakistan to crack down on Islamists since September 11.</P>
<P>"[W]e want to tell the world community...that if there is a global war against terrorism, American forces are in Afghanistan, then how can we go on tolerating terrorist acts in our country?" Vajpayee said last week.</P>
<P>Vajpayee believes that the U.S. will tilt away from longtime ally Pakistan in order to consolidate its new alliance with India--and keep New Delhi from reverting to its Cold War alliance with Russia.</P>
<P>In fact, Pakistan's President Pervez Musharraf has bowed to Washington's--and India's--demands by abandoning support for Afghanistan's Taliban government and arresting leaders of Pakistan's top five Islamist parties, along with 2,000 activists.</P>
<P>Nevertheless, Pakistan's military still has economic and political links with the Islamists. And Musharraf, who came to power in a coup, knows that if he doesn't stand up to India, the army could oust him as well.</P>
<P>With U.S. soldiers "secretly" in Pakistan to hunt for al-Qaeda forces, Musharraf figures he has room to maneuver as well. So he ordered tests of missiles capable of delivering a nuclear warhead to several of India's cities.</P>
<P>Yet when asked about the confrontation over Kashmir, Bush seemed less concerned about restraining both sides than making sure that Pakistan bowed to pressure. "I'm more concerned&#133;that President Musharraf shows results in terms of stopping people from crossing the Line of Control," he said. "Stopping terrorism. That is more important than the missile testing."</P>
<P>For Bush, the problem isn't a war in Asia, but a war that Washington doesn't wage for its own ends.</P>
<P>It's one more reason why we have to step up our opposition to Washington's war makers--and demand that the U.S. get out of the region now.</P>

