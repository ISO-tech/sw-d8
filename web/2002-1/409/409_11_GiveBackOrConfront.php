<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Concessions or confrontation?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 31, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AGGRESSIVE MOVES by employers will present a stark choice to the labor movement this summer. Will unions accept demands for concessions--some of the most aggressive in 20 years--or step up the struggle? </P>
<P>Unfortunately, instead of seizing on the outrage over scandals like the Enron collapse to mobilize a campaign for workers' rights, AFL-CIO President John Sweeney and other unions are ignoring the looming showdown. Instead, they're focused on getting Democrats--and more than a few Republicans--elected this fall.</P>
<P>Meanwhile, employers are determined to take advantage of the weakness of a union movement that today represents less than 10 percent of workers in the private sector.</P>
<P>Leading the war on unions are the airlines, where bosses want givebacks to pay for a crisis that they say was caused by September 11. In reality, the industry was losing money before the attacks--and employers planned to demand concessions anyway. US Airways, for example, wants to take back a staggering $950 million in wages, pensions and health care.</P>
<P>This recalls the early 1980s, when union concessions didn't save jobs--and only led to givebacks in union after union. In 1982, for example, the Teamsters agreed to let UPS cut starting pay from $12 per hour to $8--which didn't change until a 50-cent an hour increase following the 1997 strike.</P>
<P>Now union leaders at the Air Line Pilots Association and International Association of Machinists (IAM) are again accepting the logic of givebacks. And the United Auto Workers (UAW) kept silent about plant closures at Ford and scheduled an early bargaining convention to accommodate employers.</P>
<P>The United Steel Workers of America are collaborating with employers and George W. Bush to get restrictions on steel imports--even as steel companies use bankruptcy court to wipe out retirees' pensions and benefits.</P>
<P>In the public sector, unions such as AFSCME and SEIU, as well as teachers' unions, are facing demands for layoffs, cutbacks and work-rule concessions in cities from New York to Los Angeles.</P>
<P>While some of these unions have organized protests, most are opting for backroom discussions rather than an all-out fight.</P>
<P>A few unions have taken a harder line. At Verizon, where the Communications Workers of America (CWA) won strikes in 1998 and 2000, the union is threatening a walkout to win arbitration over layoffs.</P>
<P>The Hotel Employees and Restaurant Employees (HERE) set a June 1 strike deadline in Las Vegas and is mobilizing for big contract fights in other big cities.</P>
<P>And on the West Coast, the International Longshore and Warehouse Union has been preparing for months for a possible strike or lockout when its contract expires July 1.</P>
<P>Other union leaders have talked tough--but done little to back up their words. At UPS, where labor contracts expire July 31, Teamsters President James Hoffa promised the "best contract ever." But Hoffa, who has been close to UPS management for years, called a strike authorization vote in May on less than a week's notice--and has done little mobilize members.</P>
<P>Hoffa isn't unique. Virtually every major union leader, from Sweeney on down, embraces "partnership" with employers on the grounds that business is more successful when workers and bosses work as a "team."</P>
<P>"Partnership" was a one-way street even during the boom. Workers' gains were meager while employers grabbed enormous profits. And today, it means accepting setbacks.</P>
<P>At Boeing, for example, where the IAM won "what it considered an ironclad job-security clause in the 1999 contract, the union has since lost more than 10,000 hourly Boeing workers in the Puget Sound area to layoffs and attrition," the <I>Seattle Times</I> recently reported. Now, Boeing wants to eliminate thousands more jobs when its contract with the IAM expires September 1.</P>
<P>Such outrageous employer demands--and anger from rank-and-file union members--could compel even conservative union leaders to take action. It's time to draw the line.</P>
<P>Employers who looted their companies while shafting workers in the 1990s have no right to demand sacrifice from workers today. On the contrary, it's time for union members to fight for what's rightfully theirs--and do whatever it takes to win.</P>

