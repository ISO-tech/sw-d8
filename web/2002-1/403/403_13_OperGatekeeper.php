<P><font face="Times New Roman, Times, serif" size="5"><b>Overcoming the walls that divide us</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Justin Akers</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 19, 2002 | Page 13</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>BOOKS:</B> Joseph Nevins, <B><I>Operation Gatekeeper.</B></I> Routledge, 2002, 286 pages, $17.95. </P></font>
<font face="Times New Roman, Times, serif" size="3"><P>LOOKING SOUTH from any elevated point in Southern San Diego, one can see the teeming hillsides of Tijuana, Mexico, only minutes away. The region of Tijuana and San Diego looks like one large metropolitan center, linking beaches, freeways, factories and nearly 7 million people.</P>
<P>This, were it not for the militarized Border Wall that slices this area into separate entities, asserting that Mexicans are not welcome here. </P>
<P>In <I>Operation Gatekeeper,</I> journalist Joseph Nevins traces the development of the U.S.-Mexico border and the evolution of U.S. policy that has led to Operation Gatekeeper and the classification of Mexican workers as "illegals."</P>
<P>He begins with the Mexican-American War, when the U.S. took the Southwest--what was then Northern Mexico--through conquest. Mexicans became "foreigners" in their own land.</P>
<P>Since then, the U.S. has pursued policies toward immigrants that reflect U.S. industry's need for immigrant labor during economic boom and anti-immigrant reaction during downturns.</P>
<P>More recently, free-trade policies such as NAFTA have been a boon for multinational corporations, setting up plants along the Mexican side of the border. With an economic output of more than $150 billion annually, the border region now has an economy larger than Poland's.</P>
<P>Nevins takes on some critics of globalization who assert that nation-states and national boundaries are disappearing. He explains that, while the process of globalization may have diminished the barriers on corporations' movements across borders and strengthened their negotiating power with governments, the nation-state's role as guarantor of corporate interests and boundary enforcement has also increased.</P>
<P>Border enforcement, Nevins writes, "entails maximizing the perceived benefits of globalization while 'protecting' against the perceived detriments of increasing transnational flows--especially of unauthorized immigrants."</P>
<P>Nevins also traces the racist criminalization of immigrants. Since the late 1960s, politicians have made "law and order" issues central in an attempt to roll back the gains of the civil rights movement and the social welfare state.</P>
<P>From Republican presidents Nixon and Reagan to California Gov. Pete Wilson and Democratic President Bill Clinton, politicians have fomented an image of "out-of-control borders." Operation Gatekeeper made immigration a "national security" issue and fused the immigrant with the criminal.</P>
<P>So while corporations make out like bandits, the workers who cross over face death in the less-patrolled, rugged terrain between San Diego and Yuma, Arizona. If they make it, they receive an "illegal" political status in the U.S., which reinforces racism and violence against immigrants and deprives them of basic rights.</P>
<P>By accepting the reactionary tenets that "immigrants are part of the problem," trade unions and liberal organizations have offered no real opposition. This only weakens the labor movement, as the U.S. working class becomes increasingly made up of immigrants, especially from Mexico.</P>
<P>Nevins points to solutions in the movement for global justice and developing links between workers across borders. He embraces the movement for what he calls a "transnational integrationist vision."</P>
<P>In other words, a movement that unites the workers of the world. If Nevins is hesitant to use such plain language, that is his book's biggest flaw.</P>
<I><P>Operation Gatekeeper</I> is an excellent resource for activists seeking to overcome the walls that divide us.</P>

