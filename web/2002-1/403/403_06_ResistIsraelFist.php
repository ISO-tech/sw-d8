<P><font face="Times New Roman, Times, serif" size="5"><b>Resisting Israel's iron fist</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 19, 2002 | Page 6</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ISRAELI CIVILIANS are "murdered." Palestinian civilians are "caught in the crossfire." This is the double standard of the U.S. mainstream media. No Israeli atrocity is ever described as anything more than an overreaction to Palestinian violence, which started the bloodshed.</P>
<P>Even when Israel Defense Forces (IDF) soldiers went through the Jenin refugee camp--murdering, wreaking mayhem, humiliating residents--the media singled out a Palestinian ambush that killed 13 IDF soldiers as "terrorism."</P>
<P>Israeli Prime Minister Ariel Sharon had the gall to claim that the ambush was timed--to happen on Holocaust Remembrance Day. As if the real outrage wasn't the IDF's murderous onslaught.</P>
<P>But Sharon's rhetoric is familiar to Palestinians. Israeli leaders have always used the horrors of the Nazi Holocaust of Jews as justification for Israel's repression.</P>
<P>The true parallel works the other way--as an IDF officer admitted when he urged his fellow officers to learn the lessons of how the Nazis fought the Warsaw Ghetto uprising of Jewish fighters and apply them to crushing Palestinian resistance.</P>
<P>You'll never hear this from U.S. politicians and the pundits, of course--only denunciations of Palestinian suicide bombings. These terrible attacks are counterproductive for the Palestinian cause, giving Sharon a pretext for further violence.</P>
<P>But anyone who wants to understand the truth about this conflict needs to recognize that the suicide bombings are a cry of despair--the act of young Palestinian men, and now women, who would rather die lashing out than live under Israel's iron heel.</P>
<P>Some Israelis have come to recognize this reality. "I cannot even begin to describe the detentions, the torturing and the physical and psychological suffering that we had caused to human beings who are just like us, and which we are still causing," wrote Ofer Beit Halachmi, an Israeli reserve officer who is one of hundreds to refuse to serve in the Occupied Territories. "The deterioration of the current situation is a process, the beginning of which is an immoral and unjust rule of another people--a military rule that we established, yet under which we ourselves would never ever be willing to live."</P>
<P>Palestinians aren't willing to live under Israel's unjust rule--so they resist in any way they can. That's why we have to unconditionally support the right of Palestinians to struggle for their liberation.</P>

