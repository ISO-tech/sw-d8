<P><font face="Times New Roman, Times, serif" size="5"><b>We took a stand for justice for Palestinians</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 19, 2002 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>I'm a member of the International Socialist Organization (ISO) and a student at George Mason University--a surprisingly conservative university, especially considering its enormous international student population. </P>
<P>Since September 11, political organizing here has been an uphill battle, mostly due to the effects of attacks on our rights like the USA PATRIOT Act. Many students now have to worry about whether or not they'll be deported for speaking their minds.</P>
<P>But not even these attacks can drown out the cries for justice in occupied Palestine that have swept the world in the past few weeks. April 11 saw the emergence of those cries on my campus. </P>
<P>Students for Justice in Palestine, with help from the ISO, organized a rally to show Ariel Sharon and George W. Bush that no matter how hard they try to disregard the basic human rights of the Palestinian people, we will not sit idly by and watch genocide.</P>
<P>Shouts of "Self-determination, not occupation!" and "Free, free Palestine!" rose above the usual bustle and stopped everyone in their tracks, as more than 200 of us marched through the entire campus.</P>
<P>Classes stopped and students came outside, many running to join us. People emptied out of dorms to watch the demonstration and to join in. </P>
<P>I am convinced beyond all doubt that if we can have that kind of overwhelming success organizing to fight for Palestinian rights here, then it can happen anywhere.</P>
<B><P>Matt Hobbs,</B> Fairfax, Va.</P>

