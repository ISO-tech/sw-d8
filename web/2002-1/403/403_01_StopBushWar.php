<P><font face="Times New Roman, Times, serif" size="4">Bush's hit list: Afghans, Palestinians, Iraqis&#133;</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Stop Bush's war machine</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 19, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"SOMETIMES BRUTE force can be useful," said Lt. Gen. Ronald Kadish. He was explaining why his bosses in the Bush administration want to add nuclear weapons to their insane Star Wars missile defense scheme. But Kadish's words could just as easily be the motto for the Bush gang's drive to war around the globe since September 11.</P>
<P>The Pentagon had plenty of brute force to unleash on Afghanistan--dumping cluster bombs, cruise missiles and massive fuel-air explosives on one of the poorest countries in the world.</P>
<P>Bush exploited anger at the September 11 attacks to claim that the war on Afghanistan was necessary to bring to justice those supposedly responsible--Osama bin Laden and the al-Qaeda network. Too bad for the more than 3,000 people who happened to get in the way of U.S. bombs--even if they had nothing at all to do with Osama bin Laden.</P>
<P>But no one is even trying to stretch Bush's next targets to fit the September 11 excuse. Like Colombia, where the military with the worst human rights record in the Western hemisphere is waging a dirty war on left-wing rebels, using all the brute force that the U.S. government can supply it with.</P>
<P>No one seriously claims that Palestinians resisting Israel's occupation in the West Bank are tied to al-Qaeda. But that didn't stop Bush from giving the green light to Israeli Prime Minister Ariel Sharon's savage onslaught--an offensive justified with the same talk of fighting "terrorism" and carried out with brute force supplied, once again, by the U.S. government.</P>
<P>Then, of course, there's the favorite U.S. bogeyman: Saddam Hussein. "I made up my mind that Saddam Hussein has to go," Bush declared this month. It's just a question of figuring out when and how to apply the brute force necessary to make it happen.</P>
<P>Regardless of how many hundreds of thousands of Iraqis die. Regardless of the certain explosions of outrage across the Middle East and around the world. Regardless of any issue or principle other than what will advance U.S. power--be it military, political or economic.</P>
<P>This weekend, tens of thousands of people will come together in Washington, D.C., and San Francisco for antiwar demonstrations. We'll protest the Bush gang's drive to impose U.S. power around the globe. We'll denounce the U.S. government's support for tyranny and violence around the globe, from Ariel Sharon's reign of terror against Palestinians to the Colombian military's death-squad killers.</P>
<P>We'll oppose the priorities of a system that is preparing to pump $400 billion into the Pentagon's war machine--while our schools crumble, while our unions come under attack, while our social safety net is slashed to ribbons.</P>
<P>And when the protests are over in Washington and San Francisco, we'll take this message of resistance back to workplaces, communities and campuses. We're right to protest--to organize the fight against the Bush war machine.</P>

