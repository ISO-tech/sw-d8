<P><font face="Arial, Helvetica, sans-serif" size="2"><b>INSIDE THE SYSTEM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Everywhere you want to pay interest</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 19, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>PITY THE poor credit card companies. Having issued five cards for every man, woman and child in the United States, the credit bosses think that the market may be a bit "oversaturated." That's why Visa is finding new ways for people to use cards that they already have.</P>
<P>The company struck a deal with the biggest U.S. property manager, Denver-based Apartment Investment and Management Co. (Aimco), to allow tenants to pay their monthly rents with credit cards.</P>
<P>The move is part of a growing effort by credit card issuers to boost profits by encouraging consumers to use cards for recurring transactions, such as paying utility bills, car payments and, now, rent.</P>
<P>Of course, bigger bills mean bigger profits for the banks that issue credit cards. So tenants can be ripped off by their landlords every month--and by their credit card companies and banks.</P>
<P>Then there's the added benefit for property managers. As Visa's press release puts it, "Offering residents the automatic bill-payment option helps avoid that moment of truth each month, when residents write a check and inevitably ask themselves, 'Am I paying too much for what I'm getting? Should I look around to see if I can get a better deal elsewhere?'"</P>
<P>--<I>Los Angeles Times,</I> April 9, 2002</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Isn't Alabama a gas?</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. government sure knows how to make people feel safe. Alabama Gov. Don Siegelman recently sued to prevent a chemical weapons incinerator at the Anniston Army Depot in eastern Alabama from becoming operational.</P>
<P>The army had slated test burns of chemical nerve gas for as early as September. Not surprisingly, the 75,000 residents who live within nine miles of the plant aren't too happy with the idea of toxic clouds of smoke pouring over their neighborhoods. </P>
<P>So to make them feel safer (and to make the governor withdraw his lawsuit), the federal government has decided to issue gas masks and other protective equipment to 35,000 area residents.</P>
<P>--Associated Press, March 27, 2002</P>

