<font face="Times New Roman, Times, serif" size="3"><P>"HE BARKS a lot." That's how one member of an Israeli tank brigade waging war in the West Bank described George W. Bush.</P>
<P>But it wasn't the U.S. president that he was talking about--it was the brigade's mascot, a pit bull terrier named "George W. Bush."</P>
<P>Apparently, Bush's call for the Israeli military to stop slaughtering Palestinians--meek though it was--hit a sour note with some Israeli soldiers. When a reporter asked what the dog was like, one soldier replied, "He's useless."</P>
<P>--Reuters, April 9, 2002</P>

