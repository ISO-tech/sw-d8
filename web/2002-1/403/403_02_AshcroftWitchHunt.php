<P><font face="Times New Roman, Times, serif" size="4">Justice Department targets attorney</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Ashcroft's witch-hunt</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Wengraf</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 19, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. government opened a new front last week in its war at home. Federal prosecutors charged New York defense lawyer Lynne Stewart, along with three other people, with "unlawful communications" with Stewart's client, Egyptian Sheikh Omar Abdel-Rahman, during prison visits and telephone calls. </P>
<P>Abdel-Rahman has been held in a Minnesota prison on a life term since 1995, when he was convicted of conspiring to blow up New York landmarks, including the United Nations. Essentially, Stewart is being charged with helping to communicate Abdel-Rahman's alleged orders for future terrorist attacks.</P>
<P>But the evidence is a joke. One charge against Stewart, for example, relates to her announcement to the media that Abdel-Rahman no longer supported a cease-fire between his Islamic Group and the Egyptian government.</P>
<P>Lawyers and their clients are supposed to have special privacy rights protected by the Constitution. But John Ashcroft and his Justice Department have used the excuse of September 11 to chip away at this.</P>
<P>That's why Stewart faces as many as 20 years in prison "for nothing other than mere representation of her client," said Bill Goodman, legal division director of the Center for Constitutional Rights.</P>
<P>It's no surprise that the feds focused on Stewart. She has a long history of challenging government repression and representing political defendants in high-profile trials. "The prosecution here is designed to intimidate criminal defendants, and she's a criminal defendant," Michael Letwin, president of the Association of Legal Aid Attorneys/United Auto Workers, told <I>Socialist Worker</I>. "The government has targeted someone on the extreme, radical end. If they prosecute her, it's a warning to all lawyers and all dissenters."</P>
<P>After arraignment on April 9, Stewart was released on half a million dollars bail. Outside the courthouse, she told reporters: "The government has to put up or shut up--and I don't think they can put up&#133;I know a good fight when I see it, and I think this will be a very good fight. [Hopefully, it will be] a touchstone case that points out the limits the government can go through in prosecuting people they don't like."</P>

