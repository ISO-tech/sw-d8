<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Anarchy of the capitalist system</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 19, 2002 | Page 13</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WE ARE told that capitalism and the market are the most efficient way to distribute goods and services. State planning--as shown by the failed experiment in Russia--was an abject failure. But the question is, who's doing the planning and why?</P>
<P>There are elements of planning under capitalism--within individual firms and in state-run operations such as the post office. Managers make decisions on how to allocate labor and resources between different units within the firm.</P>
<P>But this internal planning takes place under external pressures, over which each individual firm has no control. The world market imposes on each firm the drive to cut costs and increase revenue--to exploit labor for profit.</P>
<P>But externally there is no real planning. Firms produce blindly for the market. Each firm measures its success by the amount of profit it makes (how much it takes in compared to how much it invests) and by how much its market share expands against its competitors.</P>
<P>It does this primarily by using/inventing machines that increase labor productivity, making workers work faster and giving them less money and benefits. In short, by increasing the exploitation of labor.</P>
<P>Wealth is therefore accumulated not to enrich the laborer, but at the expense of the laborer.</P>
<P>Moreover, there is no planning in the market. What the market is able to "bear" is an unknown quantity--to be determined by buying and selling. Sometimes too little of a product is produced compared to demand, and prices go up. Sometimes too much is produced, and prices plummet. Some firms go to the wall, and others expand and become larger.</P>
<P>As capitalism ages, each industry becomes dominated by a handful of giant, powerful firms. Planning grows, but it remains planning within the framework of the world capitalist market.</P>
<P>The same logic holds if the planning takes place within an entire state, as in Stalinist Russia or Castro's Cuba. The state, like an individual firm, is compelled to operate in the context of the pressures of a world market.</P>
<P>Hence in Russia, state planning was carried out in such a way as to build up a sufficient surplus to reinvest in heavy industry for building up a big military machine. The result was that workers in Russia, like those in the West, had a surplus product squeezed from them and appropriated by a ruling class. Stalinist Russia was therefore just as capitalist as the West.</P>
<P>The existence of a nationally planned economy does not in itself equal socialism. But real socialism must be based upon democratic planning for human need.</P>
<P>Supply and demand under capitalism have nothing to do with human need--only on who has the cash and the inclination to buy. In India, for example, 200 million people are malnourished. Yet in 1995, India exported $625 million worth of wheat and $1.3 billion worth of rice (5 million metric tons).</P>
<P>Every 10 years or so, there is a crisis in which the absurdity of the market is revealed. Despite widespread starvation, homelessness and deprivation of all sorts, factories become idle, goods go unsold, people are thrown out of work, social spending is cut back drastically.</P>
<P>Capitalism goes into crisis not because of scarcity, but because, as Marx pointed out so long ago, there is "overproduction." It is this economic crisis that lays bare most clearly the way in which capitalism is not in the least bit designed to serve human need.</P>
<P>Capitalism is by its nature wasteful and harmful. Massive amounts of human labor is wasted by chronic unemployment, on producing weapons that can wipe out billions of people, or on advertising for dishwashing soap.</P>
<P>Only in a system based on democratic planning--where the united producers decide what to produce, how to produce it, and the best means to distribute it--could these contradictions be overcome.</P>

