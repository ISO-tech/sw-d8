<P><font face="Times New Roman, Times, serif" size="3"><b>One child dies of hunger every seven seconds</b></font><BR>
<A HREF="403_10_PovertyAmidPlenty.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Poverty in a world of plenty</b></font></A><BR>
