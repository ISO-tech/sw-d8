<P><font face="Times New Roman, Times, serif" size="3"><b>Bush's hit list: Afghans, Palestinians, Iraqis&#133;</b></font><BR>
<A HREF="403_01_StopBushWar.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Stop Bush's war machine</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">"Sometimes brute force can be useful," said Lt. Gen. Ronald Kadish, explaining why his bosses in the Bush administration want to add nuclear weapons to their insane Star Wars missile defense scheme. But Kadish's words could just as easily be the motto for the Bush gang's drive to war around the globe.</P>

<P><font face="Times New Roman, Times, serif" size="3"><b>The massacre Israel denies</b></font><BR>
<A HREF="403_16_JeninSlaughter.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Slaughter in Jenin</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">"It is the end of life." That's how one dazed man described the carnage and destruction as he fled from the Jenin refugee camp that Israel Defense Forces soldiers turned into a slaughterhouse.</P>


<p><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>WHAT WE THINK</b></font></p>

<P><font face="Times New Roman, Times, serif" size="2"><B>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="3"><b>Washington politicians line up to justify Sharon's slaughter</b></font><BR>
<A HREF="403_03_USCheersIsrael.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Cheering on Israel's war</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">When Lebanese fascist militias massacred up to 2,000 Palestinians in the Sabra and Shatila refugee camp in 1982, an Israeli inquiry found Ariel Sharon to be "indirectly responsible." Twenty years later, Sharon is directly responsible for another massacre of Palestinians--this time in the Jenin refugee camp in the West Bank.</P>

<P><font face="Times New Roman, Times, serif" size="2"><B>WHAT WE THINK</b></font><BR>
<A HREF="403_03_USBackedCoup.shtml"><font face="Times New Roman, Times, serif" size="4"><b>A U.S.-backed coup that backfired</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">"When is a coup not a coup?" asked the <I>New York Times'</I> Tim Weiner. "When the United States says so, it seems." Weiner perfectly captured the lying-through-their-teeth hypocrisy of the Bush gang and their support for the would-be dictators who briefly seized power in Venezuela last week.</P>


