<p><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>STAND UP AGAINST ISRAEL'S WAR</b></font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>Naseer Aruri on why Oslo was doomed to fail</b></font><BR>
<A HREF="403_07_NaseerAruri.shtml"><font face="Times New Roman, Times, serif" size="4"><b>"Israel's recipe for perpetual conflict"</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">No Israeli defense of its war on Palestinians would be complete without a reference to Yasser Arafat's supposed rejection of "peace" at the Camp David summit hosted by Bill Clinton. But the facts are very different, as Naseer Aruri, author of <I>The Obstruction of Peace</I>, explains.</P>

<P><A HREF="403_06_ResistIsraelFist.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Resisting Israel's iron fist</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">Israeli civilians are "murdered." Palestinian civilians are "caught in the crossfire." This is the double standard of the U.S. mainstream media. </P>

<P><A HREF="403_06_LiberatePalestine.shtml"><font face="Times New Roman, Times, serif" size="4"><b>How can Palestine be liberated?</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">The only just solution is a democratic and secular Palestine, with equal rights for all. Achieving this will mean confronting Israel; its backer, U.S. imperialism; and the corrupt and dictatorial Arab states that collaborate with the U.S.</P>

<P><font face="Times New Roman, Times, serif" size="2"><B>WHICH SIDE ARE YOU ON?</b></font><BR>
<font face="Times New Roman, Times, serif" size="3"><b>The real source of violence in the Middle East</b></font><BR>
<A HREF="403_04_CounterIsraelLies.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Countering their lies</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">For most people in the U.S., who rely exclusively on the mass media for news, the crisis in the Middle East appears remote--a tragic "cycle of violence" that U.S. leaders have repeatedly tried to stop without success. But the mainstream news media has withheld the information most crucial to understanding the conflict.</P>

<P><font face="Times New Roman, Times, serif" size="3"><b>Protesters across U.S. say:</b></font><BR>
<A HREF="403_16_USProtestsIsrael.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Stop Israel's terror!</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">Even as U.S. politicians declared their bipartisan support for Israel, thousands of people took to the streets across the country to show their solidarity with Palestinians.</P>

<P><A HREF="403_07_WashingtonMonster.shtml"><font face="Times New Roman, Times, serif" size="4"><b>How Washington created a monster</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">No other country in the world has received as much aid or support from the U.S. government as Israel. </P>


