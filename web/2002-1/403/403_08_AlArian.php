<P><font face="Times New Roman, Times, serif" size="4">A witch-hunt victim</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"This is like fighting ghosts"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 19, 2002 | Pages 8 and 9</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"PROFESSORS HAVE the right to say things that are unpopular. But they do not have the right to disrupt the life of a university." That's what Florida's Gov. Jeb Bush told reporters in December. He was talking about Dr. SAMI AL-ARIAN, a computer science professor at the University of South Florida (USF) who was fired. Why?</P>
<P>Shortly after the September 11 attacks, Al-Arian appeared on the Fox News <I>O'Reilly Factor,</I> where right-wing host Bill O'Reilly branded him a "terrorist." Years earlier, Al-Arian, a supporter of Palestinian rights, had founded an Islamic charity.</P>
<P>The Justice Department tried in three separate investigations to tie the charity to "terrorism," but never found a shred of evidence. But that didn't stop O'Reilly from slandering Al-Arian--who began receiving hate mail and death threats after his appearance.</P>
<P>After first suspending him with pay, the school's board of trustees met on less than 24 hours' notice in December and--with neither Al-Arian nor his lawyers allowed to attend--recommended that Al-Arian be fired.</P>
<P>Since then, Al-Arian has received wide support, with the USF faculty union voting unanimously in favor of defending him. This forced USF President Judy Genshaft to delay a decision on the firing.</P>
<P>Meanwhile, Al-Arian has spent the past three months speaking out--not only in his own defense, but also to bring attention to the witch-hunt of Arabs and Muslims across the U.S. Here, Al-Arian speaks with <I>Socialist Worker</I> about the struggle against George W. Bush's war at home.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<p><font face="Times New Roman, Times, serif" size="3"><b>WHY WERE you targeted?</b></P>
<P>I WAS targeted basically because of my pro-Palestinian activism in the late 1980s and early 1990s. We were organizing conferences in support of the Palestinian Intifada, and we were criticizing policies of occupation, oppression and hegemony over the Palestinian people. And because of our success, we were targeted.</P>
<P>This is an orchestrated political campaign by people who would like to settle political scores, and they found an opening after September 11. They basically did away with the guarantees of the Constitution and the Bill of Rights. Freedom of speech, academic freedom, due process--all these values were put aside.</P>
<P>Back in 1976, when I was 18 years of age, I took my first civics course in college, and I was introduced to the idea of due process. I was fascinated by the concept.</P>
<P>I had to deal with the concept 25 years later in the case of my brother-in-law, when he was denied bail based on secret evidence.</P>
<P>And now in my case, I've been fired from my tenured position without being given any due process rights to defend myself or even address the people who were demanding my termination.</P>

<B><P>WERE YOU surprised by the attack on you and how it's been carried out?</P>
</B><P>I'M NOT totally surprised. I assumed that people would try to do that kind of thing. But I'm surprised that they were successful, in a way--that they were able to get to the Justice Department, to university presidents, to the people in the media who you would think should know better.</P>
<P>I'm surprised at their success. I'm not surprised that they tried.</P>

<B><P>WHAT DO you think about the Justice Department's insistence that it can use secret evidence? </P>
</B><P>SECRET EVIDENCE means that the prosecutor or whoever from the government's side takes the judge in a chamber without the defendant or his attorney present. They then present to the judge whatever evidence they want to present.</P>
<P>Then the judge will come back to the defendant and ask him to defend himself, without telling him anything about the nature of the evidence, what the evidence might be, what the accusations are.</P>
<P>And a defendant is supposed to defend himself against things that he hasn't seen and things he knows nothing about. This is the reality for dozens of people in INS custody.</P>
<P>We were hoping to ban this misuse of evidence, but unfortunately, after September 11, all of our attempts have been shelved. It's like fighting ghosts. It's like Kafka. It's an Orwellian-type court system. You can't believe it. </P>
<P>Anybody, even the most innocent of people--how could they defend themselves against such attacks?</P>

<B><P>WHERE DO things stand in the fight around these issues?</P></B>
<P>FOR ME personally, there has been a groundswell of support from academics and faculty and unions around the country, and I think that's slowed down the process of terminating me.</P>
<P>The university president hasn't made a final decision yet. There's an investigation by the American Association of University Professors, and although they haven't issued their report yet, I believe they'll be supporting me.</P>
<P>In terms of the larger issues at hand, we need to organize and mobilize for a new civil rights movement.</P>
<P>It's very important for people to understand that Muslims in America are against what happened on September 11. They condemned it in the strongest possible terms and were horrified by it.</P>
<P>And now they're being made victims because of September 11.</P>

