<P><font face="Times New Roman, Times, serif" size="5"><b>HERE targets Claremont hotel spa</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jessie Muldoon</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 19, 2002 | Page 15</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>BERKELEY, Calif.--Workers at the Claremont Resorts and Spa are united in a fight for union rights and justice on the job. Although the majority of the hotel's employees are unionized with HERE Local 2850, about 140 employees who work in the spa and salon are not.</P>
<P>The Claremont provides union members, such as dishwashers, cooks, bussers and servers, with free health care for working 20 hours a week. But spa and salon employees must work 32 hours a week and pay between $150 and $300 a month for family health care.</P>
<P>The Claremont charges clients between $50 and $300 for various spa and salon services. Workers make just $6.75 an hour. In addition, workers have repeatedly tried and failed to get management to account for charged and cash tips left for them.</P>
<P>Workers are asking to be treated with dignity and respect. "I am organizing to be in the union because I am setting an example for my family when respect, fairness and dignity are compromised," said Norine Madrid, an aesthetician at the spa for nine years.</P>
<P>Since workers went public with their union campaign in January, many have signed union authorization cards, and have repeatedly asked that the company simply count their cards. But Claremont refuses.</P>
<P>Management is trying to divide and conquer workers by forcing separate department meetings and having one-on-one meetings with employees. They suspended four workers for eight days for leafleting customers and community members on their own time. Charges for this illegal conduct have been filed with the labor board.</P>
<P>Union members recently began bargaining for a new contract with Claremont's parent company, KSL Resorts. KSL is offering 2.5- to 10-cent raises and $300-a-month co-payments for health insurance. This is a slap in the face to workers who have been at the Claremont for decades--especially since KSL owns eight resorts, having purchased one recently in La Costa for $150 million.</P>
<P>Union members have signed a petition to support their sisters and brothers in the spa and salon, and now all of the workers are united in the fight for just wages and health care. The Oakland and Berkeley city councils have passed resolutions in support of the workers.</P>
<P>Workers are planning a "Guess Who's Coming to Brunch" rally at the Claremont on April 28.</P>
