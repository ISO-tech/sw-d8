<P><font face="Times New Roman, Times, serif" size="5"><b>Demand justice for the Hamoui family</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Darrin Hoop</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 19, 2002 | Page 15</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SEATTLE--Activists are holding weekly protests at the Immigration and Naturalization Services (INS) jail here to demand freedom for the Hamoui family.</P>
<P>Safouh, Hanan Ismail and their 19-year-old daughter Nadin have been locked up for almost two months under Attorney General John Ashcroft's "Absconder Apprehension Initiative." The policy gave the green light for the racial profiling of thousands of Arab Americans.</P>
<P>On February 22, the first day of the Islamic holiday Eid, at 6 a.m., INS and FBI agents knocked on the family's door. When Safouh opened the door, authorities with guns drawn began scouring the house. Hanan Ismail, a mother of five kids, was awakened with a flashlight and a gun pointed in her face.</P>
<P>From then on, she has been treated horribly. Authorities refused to allow her to wear her traditional hijab on the way to the jail. She was handcuffed to a chair for four hours, because she refused to wear the prison garb without her other clothes.</P>
<P>Hanan Ismail has Crohn's disease, a gastrointestinal disease that worsens with stress and an unhealthy diet. Since being detained, she has been taken to the emergency room five times.</P>
<P>The Hamouis are separated from their four other kids. The youngest is 11 years old. The INS has tried to deport either a portion of or the entire family twice.</P>
<P>The Hamouis moved here from Syria in 1992, and they've been trying to file for political asylum ever since. The INS claims they overstayed their visas. But the Hamouis hired several different lawyers who never filled out the political asylum forms properly.</P>
<P>Several local politicians have called on the INS to let them go. Organizers of the weekly protests--relatives, the Arab American Community Coalition (AACC) and others--know that the only way to free the Hamouis is to put pressure on the INS to let them go.</P>
