<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">The real source of violence in the Middle East</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Countering their lies</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 19, 2002 | Page 4</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>FOR MOST people in the U.S., who rely exclusively on the mass media for news, the crisis in the Middle East appears remote--a tragic "cycle of violence" that U.S. leaders have repeatedly tried to stop without success. Not surprisingly, therefore, most opinion polls show strong majorities calling for an end to violence on both the Israeli and Palestinian sides, as equal culprits in the bloodletting.</P>
<P>But the round-the-clock "crisis coverage" of the mainstream news media has withheld the information most crucial to understanding the conflict: The current escalation of violence is taking place after 35 years of illegal Israeli occupation of Palestinian lands--the second-longest occupation in modern history.</P>
<P>The state of Israel itself was founded on the forcible expulsion of 68 percent of the Palestinian population from their native land in 1948, with 4.5 million remaining refugees today.</P>
<P>The Gaza strip, about twice the size of the District of Columbia, is the most densely populated area on earth, crammed with more than 1.3 million Palestinian refugees, while nearly 2 million Palestinians jam the West Bank, an area the size of Delaware. Israel controls the Occupied Territories' entire water supply.</P>
<P>Unemployment among Palestinians in the Occupied Territories has soared to 60 percent, and half live on less than $2 a day. They live in 63 cantons--each completely encircled by Israeli troops--with no right to travel from one place to another. When they do, they must travel only on "non-Jewish" roads, and endure long lines and humiliation at Israeli checkpoints--held prisoner in their own land. </P>
<P>Far from brokering peace, the U.S. has consistently backed Israel as it has flouted one UN resolution after another--dating back to 1967, when it condemned Israel's occupation, carried out with a war machine armed and paid for by the U.S. government.</P>
<P>Israel--a nation with one of the world's highest per capita incomes and a universal health care system--is also the world's highest recipient of U.S. aid, receiving more than $5 billion annually. U.S. tax dollars have built the Israeli military into one of the five deadliest in the world and the only nuclear power in the Middle East.</P>
<P>Some of the U.S. money has been spent to build Israeli settlements on Palestinian land, in flagrant violation of international law. During the eight years after the 1993 Oslo peace agreement, the number of Israeli settlers increased by 70 percent--with their prosperous homes and swimming pools in full view of the impoverished Palestinians.</P>
<P>Israel also channels U.S. money into public relations firms, which are paid to focus media attention on Palestinian suicide bombers who kill Israeli civilians, while ignoring Israeli settler fanatics who routinely kill unarmed Palestinian civilians, including women and small children, and steal their land.</P>
<P>Most Americans would be appalled to learn how their tax dollars have been used by the Israeli military in its assault on the West Bank. The tanks carrying Israeli soldiers as they shoot down Palestinian civilians are U.S. made and bought by Israel using U.S. aid, while its planes and helicopters are state-of-the-art Black Hawks, F-16s and Apaches.</P>
<P>As they savagely attacked Palestinian refugee camps throughout the West Bank, Israeli soldiers blew up electric transmission lines and deliberately destroyed drinking water supply lines. At the Jenin refugee camp, where Palestinians claim 500 residents were massacred, people were forced to drink water from sewers so as not to die of thirst.</P>
<P>The Israel Defense Forces banned all ambulances for the wounded and dying, and all burials, so the survivors trapped in their homes were surrounded by the rotting corpses of their loved ones. And the Israelis banned the media, so there would be no outside witnesses to the carnage.</P>
<P>That is how Israel put U.S. tax dollars to work in the most recent weeks--in a campaign of ethnic cleansing against the Palestinian population, in which Israelis murdered hundreds and suicide bombers murdered fewer than 30.</P>
<P>There is one solution to the crisis, which the U.S. media never warrants a mention. Withdraw U.S. funding of the Israeli war machine and U.S. backing of Israel's illegal occupation.</P>

