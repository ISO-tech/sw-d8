<P><font face="Times New Roman, Times, serif" size="4"><b>Don't mourn the death of a parasite</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>Since the death of Britain's "Queen Mother," the mother of Queen Elizabeth, the press has bombarded us with tributes to this "beacon of hope and courage" who was "surprisingly common."</P>
<P>Are they joking? The woman was a gin-swigging parasite who led a life of incredible luxury that we "commoners" can't even begin to imagine. </P>
<P>The media praised her for refusing to leave London during the bombing in the Second World War. But the truth is that while ordinary people were forced to sleep in open fields to escape the bombs, she spent her nights in the safety and comfort of Windsor Palace, far from London.</P>
<P>At one point during the war, she thought she would raise the morale of people whose homes and lives had been destroyed by Nazi bombing by "gracing" the poor in the East End of London with her presence. Instead, people hurled bricks and spat at her as she passed by.</P>
<P>Even though she never worked a day in her life, she was worth about $100 million--and still received approximately $1 million a year in taxpayers' money! Yet despite her massive wealth, she was notoriously stingy and refused to pay her servants a living wage. </P>
<P>A bigot who supported making concessions to Hitler and South African apartheid, the Queen Mother referred to Blacks as "nig nogs" and mourned the decline of British imperialism. </P>
<P>Like the rest of the royal family, the Queen Mother had nothing in common with regular people. For working-class people, hers is not a death worth mourning.</P>
<B><P>Susan Fitzgerald,</B> Boston</P>

