<p><a name="Kudos"></a><font face="Times New Roman, Times, serif" size="4"><b>Kudos to <I>SW</I> for the good work</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Kudos for your coverage of the "war on terrorism" as well as the continued Israeli war on the Palestinian people.</P>
<P>As a human services worker, I find it appalling that this government, which some people call a "democracy," can spend countless billions of dollars on death and destruction, and yet my own job--funded by the Department of Mental Health--could be axed in this year's round of Republican budget cuts.</P>
<P>This regime doesn't care about the clients I serve--mentally ill people. Their budget cuts could put my clients and me out on the street. These politicians care only about one thing: more money for the rich.</P>
<P>It all goes to show what the priorities are in this country, and how important it is that we struggle for a socialist tomorrow.</P>
<P>I've been a reader of <I>SW</I> for nearly 10 years, and the paper never disappoints me. Keep up the good work!</P>
<B><P>Bruce Burleson,</B> Boston, Mass.</P>

