<p><a name="better"></a><font face="Times New Roman, Times, serif" size="4"><b>He fought for a better society</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>On March 31, Phil Hellesto, a member of the Marine Engineers' Beneficial Association (MEBA) and an activist for labor and human rights, died as a result of an engine-room fire aboard the ship Cape Horn while it was 800 miles from Honolulu. </P>
<P>Phil was an active member of the Bay Area Charleston Five support committee. He also played an active role during the anti-World Trade Organization protests in Seattle in November 1999. In 1988, Phil was part of a delegation of U.S. union activists who went to El Salvador to provide protection to Salvadoran union leaders targeted for assassination. </P>
<P>Phil believed that unions had an important role to play in the struggle to build a better society--beyond simple "bread-and-butter issues." His longtime friend and union brother Paul Norman described Phil as someone "who lived for unionism" and "who walked the talk."</P>
<P>Those of us in the struggle for labor and human rights can best honor his memory by continuing the struggle.</P>
<B><P>Ken Morgan,</B> San Francisco</P>

