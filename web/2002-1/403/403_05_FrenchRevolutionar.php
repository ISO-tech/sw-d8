<P><font face="Arial, Helvetica, sans-serif" size="2"><b>FRANCE</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Revolutionary shakes up French vote</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 19, 2002 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>A REVOLUTIONARY socialist candidate is shaking up France's presidential election by gaining 10 percent support in opinion polls ahead of the national vote on April 21.</P>
<P>Arlette Laguiller, of the organization Lutte Ouvri&egrave;re (Workers' Fight), calls for a freeze on layoffs, limits on profits, higher wages, improved retirement benefits and better public services. Her almost nightly meetings in cities across France attract hundreds of workers--where Laguiller makes it clear that change won't come through the ballot box, but through struggle.</P>
<P>Laguiller, a retired bank teller, has put enormous pressure on Prime Minister Lionel Jospin of the Socialist Party, who is trying to unseat conservative President Jacques Chirac. In the aftermath of a public-sector general strike in 1995, Jospin's "Plural Left" government won parliamentary elections in 1997 after promising to halt the previous right-wing government's program of budget cuts and privatization of state-owned companies.</P>
<P>Jospin did make good on one promise--a mandatory 35-hour workweek. But although employers opposed this concession, they used it to impose "flexible" work hours to their own advantage. Meanwhile, Jospin appeased business by cutting corporate taxes and continuing with privatization.</P>
<P>Chirac has attacked Jospin by whipping up hysteria about crime and immigration. Jospin's response has been to announce a law-and-order campaign of his own. Appealing to the middle class, he has offered only a vague "different way of presiding over France" and declared that his program was "not socialist."</P>
<P>No wonder a Louis Harris poll found that 75 percent of people see no major differences between Jospin and Chirac. In recent days, however, Laguiller's solid support forced Jospin to declare, "I am a man of the left."</P>
<P>Lutte Ouvri&egrave;re, was founded on the politics of the Russian revolutionary Leon Trotsky. It has a significant presence in the unions. While Laguiller hasn't focused on internationalism or social issues, she criticizes the war in Afghanistan and denounces Israel's "war on the Palestinians."</P>
<P>Laguiller's votes will likely eclipse those of the French Communist Party, formerly one of the strongest in Western Europe. Her total may also surpass that of National Front Nazi Jean-Marie Le Pen, who got 15 percent of the vote in the last presidential election in 1995.</P>
<P>The winners of the first round--probably Jospin and Chirac--will face one another in a runoff election May 5. Whatever the final result, the strength of Laguiller's campaign has shown the appeal of revolutionary socialist politics in one of the world's most powerful countries.</P>

