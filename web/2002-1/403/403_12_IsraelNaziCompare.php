<P><font face="Times New Roman, Times, serif" size="5"><b>You can't compare Israelis and Nazis</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 19, 2002 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker, </P>
</B></I><P>In a recent editorial (<I>SW</I>, April 12), you compared Palestinian suicide bombers to Jews who resisted the Nazis during the Warsaw Ghetto uprising, and quoted an Israel Defense Forces (IDF) officer talking about how the lessons of that battle have been put to use to put down the Palestinian resistance.</P>
<P>Even if you took this comparison between the Warsaw Ghetto and Palestine from the words of an IDF officer, it only--I repeat ONLY--has an anti-Semitic implication. </P>
<P>There are no concentration camps in Israel/Palestine. The Israeli forces are not occupying a foreign country (it is much more complex). The Israelis are not thousands of miles from their native soil, and most of all, they are not determined to exterminate the Palestinian population as the Nazis and their Polish friends were determined to exterminate the Jews. </P>
<P>Your comparison can only fuel anti-Jewish feelings--which here in France have led manipulated individuals to burn synagogues and beat up Jews in the street.</P>
<P>It is one thing to be in favor of a Palestinian state, but it is another to support uncritically the corrupt dictatorship of Yasser Arafat and his allies. It is one thing to stand in favor of democratic rights for Palestinians, but it is another to think that terrorism is only the product of Israel's expansionist policy. </P>
<P>The religious forces (Hamas, Jihad) and nonreligious forces (PLO, etc.) make apologies for "martyrs"--meaning people who blow up innocent Israeli civilians. This has nothing to do with socialism.</P>
<B><P>Yves Coleman,</B> France</P>

