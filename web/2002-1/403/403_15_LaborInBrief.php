<p><a name="Stanford"></a><font face="Times New Roman, Times, serif" size="4"><b>Stanford University living wage</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Anna Letitia Mumford</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>PALO ALTO, Calif.--Hundreds of students and workers rallied to demand that Stanford University improve conditions for its workers. Protesters marched through campus, delaying the administration's annual "Community Day" celebration.</P>
<P>Marcher Estela Diaz said she works at the campus food court, earning $10.50 per hour without health care benefits. She and her husband are unable to pay for health insurance for their three kids because her salary goes for the rent for her East Palo Alto home.</P>
<P>The rally was organized by the Coalition for Labor Justice, which is composed of student organizations, faculty and organized labor. The coalition has been organizing for four months.</P>
<P>Six students were arrested last fall during a protest over the university's decision to subcontract custodial jobs at the Stanford Medical Center to nonunion employers. Since then, the coalition has begun to pressure the university to adopt a four-plank code of conduct, which includes higher minimum-wage standards, health benefits for all employees, a reduction in the number of jobs given out to subcontractors and university neutrality in labor contracts.</P>

