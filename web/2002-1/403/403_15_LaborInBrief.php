<p><a name="Maritime"></a><font face="Times New Roman, Times, serif" size="4"><b>Pacific Maritime Association</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SAN FRANCISCO--The International Longshore and Warehouse Union (ILWU) could be heading for a strike this summer. The bosses' organization, the Pacific Maritime Association (PMA), is out to radically transform work rules to break the power of the union as contract talks begin.</P>
<P>"Our terminals are becoming full," PMA boss Joseph Miniace told reporters. "Greater efficiency would increase the flow." Translation: The employers want to use new technology to break the power of the union, which represents some 10,500 longshore workers.</P>
<P>But Miniace has so far stonewalled the union on the details of the plan--leading the ILWU to seek an unfair labor practice ruling in courts. "When we ask for that information, he says he doesn't have it," ILWU President James Spinosa wrote to members. "Either he is lying to the press or he is lying to us." </P>
<P>Moreover, the PMA is pushing the proposed Maritime and Port Security Act, which includes background checks and mandatory drug tests for all longshore workers--and bans on those who don't pass such tests.</P>
<P>Management is also counting on internal divisions in the union to work to its advantage. Spinosa, who won election two years ago, is much more conservative than his predecessor, Brian McWilliams. But the ILWU is closing ranks to take on the employers.</P>
<P>The union joined the International Dockworkers' Council at the recent Charleston Five victory rally in Charleston--and are working to build worldwide solidarity in case of a strike.</P>

