<P><font face="Times New Roman, Times, serif" size="4"><b>Lockheed Martin</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Tony Kin</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MARIETTA, Ga.--More than 200 strikers and supporters rallied outside the Lockheed Martin plant last April 11 as union and company negotiating committees met for the second day with federal mediators.</P>
<P>One month ago, members of IAM Local 709 went on strike for the first time since 1977 after turning down a contract that failed to provide job security or improve health and retirement benefits. The Marietta plant produces F-22 fighters and C130J transports for the U.S. military.</P>
<P> "The company has not budged at this point," Local 709 Vice President Jim Farist told strikers at the rally. "There has been no movement on the company's part."</P>
<P>Lockheed is refusing to change any of the contract language and says that the current proposal, which includes a 10 percent raise over three years, is final. But as one striker noted, "You can make more than $100 an hour, but it doesn't mean anything if you don't have a job."</P>
<P>Many Lockheed workers, like Ann Marie Orr, are determined to remain on strike for as long as it takes to get a fair contract. "Why do they [Lockheed] get these great big bonuses by taking work away from us?" Orr, who has worked at Lockheed for 20 years, said. "It isn't fair and it isn't right."</P>
<P>Dave Barlow, who has worked at Lockheed since 1962, pointed out that the plant's workforce has decreased from 20,000 in the 1980s to 7,000 today because of outsourcing. "But what I want to ask management is, 'Where's your outsourcing at? How many management jobs have been outsourced?'"</P>
<P>Local 709 President Jim Carroll said, "Lockheed Martin is only proving that corporate greed knows no bounds. In a time when profits will be going through the roof, they have forgotten the workers who will make that profit possible."</P>
<P>That's why we need to stand with the strikers at Lockheed in their struggle--and with workers everywhere who are taking on the bosses.</P>

