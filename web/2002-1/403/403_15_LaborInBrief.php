<p><a name="Illinois"></a><font face="Times New Roman, Times, serif" size="4"><b>University of Illinois&#151;Chicago</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Dennis Kosuth</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--The Graduate Employees Organization (GEO) at the University of Illinois-Chicago (UIC) held two actions this week to demand that the administration recognize their union.</P>
<P>The common excuse given by the university is that teaching assistants are students and therefore cannot belong to a union. This completely disregards all the necessary work that is done by them in the daily operation of the school.</P>
<P>Discussion sessions taught by graduate students were held outside for two days to demonstrate the amount that they contribute as university workers. At the end of each day, rallies were held and speakers from many different unions, on and off campus, gave solidarity greetings.</P>
<P>Other issues were also raised. One of them was by Diversity in Demand, an organization centered on increasing the number of tenured Black faculty, which is an embarrassingly low 2 percent. A classroom instructor also spoke about how UIC plans to lay off hundreds of teachers, thereby increasing class sizes, while simultaneously upping our tuition.</P>
<P>The administration is attacking students and workers at UIC on many different fronts.</P>

