<P><font face="Times New Roman, Times, serif" size="5"><b>How Washington created a monster</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Anthony Arnove</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 19, 2002 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NO OTHER country in the world has received as much aid or support from the U.S. government as Israel. Every year, the U.S. sends billions of dollars to Israel in the form of grants, low-cost loans and subsidies. And U.S. manufacturers are always ready to supply Israel with more weapons.</P>
<P>When Israel bought nine of Boeing's deadly AH-64D Apache Longbow attack helicopters in February 2001, the <I>Jerusalem Post</I> noted that Israel "will be paying for the $500 million deal with U.S. military grant money."</P>
<P>As the <I>St. Louis Post-Dispatch</I> reported in May 1999, "Israel has acquired 260 of [Lockheed Martin's] F-16s over the years, consisting of 210 new planes and 50 used ones from the U.S. armed forces. "That's the largest fleet of F-16s anywhere in the world outside the U.S. Air Force." And the <I>Post-Dispatch</I>'s calculation appeared two months before Israel purchased another 50 F-16s--in a $2.5 billion deal paid for with U.S. funds.</P>
<P>Other U.S. aid goes toward Israel's settlements and its construction of Jewish-only roads. Then there's the indispensable role that the U.S. government has played--regardless of who is in the White House--in defending Israel when it comes under international criticism for its abuses of Palestinian rights.</P>
<P>Some cynically argue that the pro-Israeli lobby "dictates" policy in Washington. But the truth is that the U.S. backs Israel to the hilt because it wants a powerful ally in the oil-rich and volatile Middle East. Israel's role is to serve as a "watchdog" for U.S. interests.</P>
<P>In 1967, after Israel seized large areas of land from neighboring Arab countries in a war that showed off its overwhelming military power, <I>Newsweek</I> magazine said that Israel had proved its usefulness in helping the U.S. to "neutralize the Middle East, so that its oil can be profitably marketed and its waterways used for the benefit of world commerce."</P>
<P>Rabbi Baruch Korff explained the relationship another way. Noting that the openly anti-Semitic Richard Nixon was highly supportive of Israel, Korff observed that "Israel costs the U.S. less than the Sixth Fleet."</P>
<P>Of course, the U.S. government doesn't rely only on Israel to do its dirty work. It provides billions in aid and military assistance to corrupt Arab regimes--Egypt and Saudi Arabia in particular--to help keep the region under control.</P>
<P>But Israel's population welcomes its relationship to the U.S., whereas many Arabs are outraged by the ties between their government and the U.S. This makes Israel a far more reliable partner.</P>
<P>That's why it's so important that activists call for an end to the U.S. sponsorship of Israel. Palestinian deaths are bought and paid for in Washington.</P>

