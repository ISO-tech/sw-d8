<P><font face="Times New Roman, Times, serif" size="5"><b>King's stand against a U.S. war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 19, 2002 | Pages 8 and 9</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>ALAN MAASS remembers Martin Luther King's "Beyond Vietnam" speech 35 years later.</P></B>
<P>THE <I>WASHINGTON Post</I> declared that he had "diminished his usefulness to his cause, his country, his people." <I>Time </I>magazine called his words "demagogic slander."</P>
<P>The object of this abuse was a man who is celebrated today as a hero--Martin Luther King. Not 35 years ago, though--when the civil rights leader gave an antiwar speech on April 4, 1967, at New York City's Riverside Church.</P>
<P>Even then, a decade after the U.S. got involved in Vietnam, King's speech made him part of a small antiwar opposition--and an enemy of the still-united Washington establishment. But the time had come to speak out, King declared.</P>
<P>"We were taking the Black young men who had been crippled by our society and sending them 8,000 miles away to guarantee liberties in Southeast Asia which they had not found in southwest Georgia and East Harlem," he said. "So we have been repeatedly faced with the cruel irony of watching Negro and white boys on TV screens as they kill and die together for a nation that has been unable to seat them together in the same schools."</P>
<P>King freely admitted that his ideas had changed because of discussions with African American youth who had come to question King's insistence on nonviolent protest. "They asked if our nation wasn't using massive doses of violence to solve its problems, to bring about the changes it wanted," he said. "Their questions hit home, and I knew that I could never again raise my voice against the violence of the oppressed in the ghettos without having first spoken clearly to the greatest purveyor of violence in the world today--my own government."</P>
<P>Like other leaders of the movement, King had seen the civil rights struggle as an effort to fix an essentially just system in the U.S. But the years of battles--not only against racist Southern Dixiecrats, but also the national political establishment--led King to begin making connections between the struggles against racism, war and imperialism, and poverty and class inequality.</P>
<P>"On the one hand, we are called to play the good Samaritan on life's roadside," King said at the end of his speech. "But that will be only an initial act. One day, we must come to see that the whole Jericho road must be transformed, so that men and women will not be constantly beaten and robbed as they make their journey on life's highway. True compassion is more than flinging a coin to a beggar; it is not haphazard and superficial. It comes to see that an edifice which produces beggars needs restructuring."</P>

