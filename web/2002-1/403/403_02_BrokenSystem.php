<P><font face="Times New Roman, Times, serif" size="4">Why didn't Illinois report recommend abolition?</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>A broken system that can't be fixed</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 19, 2002 | Page 2</font></P>
<P><font face="Times New Roman, Times, serif" size="3">AFTER TWO years of research, the Illinois commission assigned to study the state's broken death penalty system released its findings this week. The report proposes what amounts to a complete overhaul of capital punishment--with 85 suggested reforms covering everything from police investigations to the sentencing phase of capital trials. </P>
<P>But the commission refused to call for abolition of the death penalty, even though a majority of members voted in support of this earlier in their deliberations, according to press reports. "The commission was unanimous in the belief," the report concludes, "that no system, given human nature and frailties, could ever be devised or constructed that would work perfectly and guarantee absolutely that no innocent person is ever again sentenced to death."</P>
<P>But this is exactly why the death penalty is too flawed to fix--and should be done away with for good. The commission's statement is a politically convenient compromise crafted to let politicians off the hook in an election year.</P>
<P>In January 2000, pro-death penalty Republican Gov. George Ryan surprised the nation when he imposed a moratorium on executions. Ryan was reacting to growing pressure after the 13th innocent man in 12 years was exonerated and freed from death row.</P>
<P>Ryan appointed a 14-person panel of lawyers, judges and politicians--both pro- and anti-death penalty--to study capital punishment in Illinois. Their 207-page report points to all of the flaws that death penalty opponents have long described--arbitrary application of death sentences, misconduct and abuse by police and prosecutors determined to get convictions, racism in convictions and sentencing, use of the death penalty against the mentally retarded, prosecutors' use of "snitch" testimony and single witnesses to get convictions.</P>
<P>But the commission shied away from the obvious conclusion. "In medical terms, our report calls for triage--an attempt to stanch the extraordinary rate of errors, reversals and mistaken conviction," commission co-chair Tom Sullivan admitted at a press conference on April 15. "The message of this report is clear: repair or repeal. Fix the capital punishment system or abolish it."</P>
<P>But there are plenty of pro-death penalty politicians who want neither. Republican State Sen. Kirk Dillard, vice chair of the Judiciary Committee, promised that many of the commission's proposals are "headed straight for the trash bin."</P>
<P>Death penalty opponents can't let this report be the end of the matter--or some future Illinois governor will restart the machinery of death. We have to keep up the pressure for abolition of this sick system.</P>

