<P><font face="Times New Roman, Times, serif" size="4">Protesters across U.S. say:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Stop Israel's terror</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 19, 2002 | Page 16</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>EVEN AS U.S. politicians declared their bipartisan support for Israel, thousands of people took to the streets across the country to show their solidarity with Palestinians. Demonstrations drew hundreds and even thousands in large cities like San Francisco, Chicago and New York.</P>
<P>In San Francisco, 600 rallied at the Israeli Consulate on April 5. Some Palestinian families traveled for hours to join the protest. "I think there's a solution to this madness, where Palestinians and Jews can live together in peace," Mike, a Palestinian shop owner who ran from his store to join the rally, told <I>Socialist Worker</I>. "But first, we must speak out and stop what is happening to our people now." In Chicago, a demonstration of 4,000 marched to the Israeli consulate in a fierce show of solidarity.</P>
<P>Even in smaller cities and towns, bigger-than-expected crowds turned out to demand an end to Israel's slaughter. In San Diego, Calif., 500 pro-Palestinian protesters rallied on April 7--and another 700 came together just a week later. One hundred fifty rallied at the Federal Building in Rochester, N.Y., on April 8. Two days later, a demonstration of 100 marched through downtown Providence, R.I. </P>
<P>One focal point for the movement in solidarity with Palestinians has been on college campuses, where there has been a flurry of activity in recent days. As part of Palestine Solidarity Day on April 9, activists at numerous schools held actions.</P>
<P>At the University of California-Berkeley, 79 people were arrested by police after occupying a campus hall for several hours in a peaceful protest. Demonstrators came to the jail and chanted outside until the 79 were released.</P>
<P>On the same day in Davis, Calif., 250 students gathered at the University of California-Davis to call for a free Palestine. On April 10, 350 turned out at the University of Madison-Wisconsin for a speak-out organized by a coalition of student and local activist groups. </P>
<P>The same day at Northwestern University in Evanston, Ill., 40 students picketed the appearance of David Roet, Israel's Consul General for the Midwest--who had been invited by the administration to speak on "Democracy after September 11." "I'm here because what's going on in Israel right now is that Palestinians are being murdered," one protester told <I>Socialist Worker</I>. "Israeli troops are bulldozing homes and killing our families."</P>
<P>The next day, 200 students from the University of Illinois-Chicago attended a speak-out, with students expressing the need to counter the mainstream media's lies and half-truths about the conflict.</P>
<P>At Columbia University in New York City, 180 people packed a teach-in on "Justice for Palestinians" on April 10. Speakers described the role that Israel plays as the U.S. "watchdog" in the Middle East and spoke about the resistance to the Israeli government's policies--by both Palestinians and a growing number of Jews around the world.</P>
<P>Activists are vowing to keep up the fight. On several campuses, students formed chapters of Students for Justice in Palestine to help build the fight for Palestinian rights on their campuses--and to put pressure on administrators to divest from companies that invest in Israel.</P>
<P>Students at UC-Berkeley are planning "Stand up for Justice Week." And at Columbia, activists are mobilizing for a student walkout on April 17 at 10 a.m. "As members of an esteemed academic institution, we believe that we all have a responsibility to speak out in the face of injustice," said organizers. </P>
<P>These protests show the potential for building a movement in the U.S. that calls on Washington to cut its ties to Israel's terror--and that stands up for justice for Palestinians.</P>

