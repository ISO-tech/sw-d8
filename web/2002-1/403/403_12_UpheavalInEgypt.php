<P><font face="Times New Roman, Times, serif" size="4">Mass demonstrations in defense of the Intifada</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The upheaval in Egypt</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 19, 2002 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>On April 8, at Alexandria University in Egypt, security forces killed a student demonstrator and injured dozens in a crackdown on one of the many massive protests that have swept the country in solidarity with the Palestinian Intifada. </P>
<P>Students from colleges, high schools and even middle schools--as well as thousands of unionists--have taken to the streets to demand that the Egyptian government cut diplomatic and economic relations with Israel. </P>
<P>On April 1, 20,000 people turned out to a protest called by the Popular Committee in Solidarity with the Intifada. Security forces tried to prevent protesters from reaching the Israeli embassy, which is located five minutes away from Cairo University. Al Jazeera television estimated that on that day alone, more than 1 million people protested across the country.</P>
<P>Under pressure, President Hosni Mubarak announced that his regime would cut diplomatic ties with Israel to a bare minimum. But meanwhile, the government continues to sell oil, cement and natural gas to Israel. Israel uses the oil in its F-16s and Apache helicopters to bomb Palestinian towns and cities--and uses the cement to build more settlements in the Occupied Territories.</P>
<P>Many protesters are realizing that the fight for Palestinian liberation is connected to the fight for democracy at home and an end to U.S.-Israeli domination of the region. That's why most protesters reject the attempts by Arab regimes to push initiatives such as the Saudi peace plan, which would forfeit the Palestinian right of return and leave Israel's colonial-settler state intact. </P>
<P>Many people angered by the increasing gap between rich and poor because of the failures of the IMF privatization and austerity programs are beginning to see that Arab regimes are junior partners in a system where Israel plays the role of America's main watchdog to secure control over oil.</P>
<P>That's why people say that the struggle is not only in Ramallah--but must go through Beirut, Cairo and Amman.</P>
<B><P>A protester in Egypt</P></B>

