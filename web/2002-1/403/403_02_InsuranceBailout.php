<P><font face="Times New Roman, Times, serif" size="5"><b>Bailout for the insurance bosses</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 19, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>YOU CERTAINLY can't accuse George W. Bush of letting his never-ending "war on terror" get in the way of his never-ending quest to line the pockets of Corporate America.</P>
<P>At a media event at the White House last week, Bush shamelessly stumped for Congress to enact a federal "backstop" for the insurance industry to issue terrorism insurance to real estate developers and property owners. </P>
<P>After paying out $20 billion in connection with damages from the September 11 attacks, the insurance giants claim that they can't afford a repeat. But the Bush-backed "backstop" legislation would authorize government payments up to $100 billion for damages from terrorism--while the insurance bosses line their pockets by charging exorbitant premiums.</P>
<P>Unfortunately, some labor leaders bought the line that the terrorism insurance "crisis" has led to a slowdown in new construction. Teamsters President James P. Hoffa and AFL-CIO Construction and Building Trades Department President Edward Sullivan appeared alongside Bush at his press conference.</P>
<P>But the Bush gang doesn't care about workers. They and their corporate donors have their eyes on more big bucks for corporate welfare.</P>

