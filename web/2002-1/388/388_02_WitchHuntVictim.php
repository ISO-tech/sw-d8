<P><font face="Times New Roman, Times, serif" size="5"><b>Witch-hunters claim new victim</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 4, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE WITCH-HUNT against Arab Americans reached a new stage in December when the University of South Florida (USF) fired computer science professor Dr. Sami Al-Arian.</P>
<P>After the September 11 attacks, Al-Arian appeared on Fox News' <I>O'Reilly Factor,</I> where right-wing host Bill O'Reilly branded him a "terrorist." The evidence? Al-Arian was investigated by the Justice Department six years before, though the feds found no evidence of wrongdoing.</P>
<P>Yet thanks to O'Reilly's "guilt-by-association" slander, Al-Arian was targeted for hate mail and death threats at USF.</P>
<P>Administrators suspended him with pay. But that wasn't enough for the board of trustees. In late December, the board met on less than 24 hours' notice to take up a proposal to fire Al-Arian. Neither Al-Arian nor his lawyers were allowed to attend, and none of his supporters could speak.</P>
<P>Meanwhile, as one person who attended the meeting described it, "The Board chair repeatedly referred to Dr. Al-Arian as a terrorist, implying that he was responsible for recent events in Israel."</P>
<P>Al-Arian was fired on the grounds that his presence--not anything he ever did or said, but his mere presence--was "disrupting university operations."</P>
<P>"Professors have the right to say things that are unpopular," sleazebag Florida Gov. Jeb Bush said in a statement supporting the decision. "But they do not have the right to disrupt the life of a university&#133;The taxpayers have no obligation to continue paying a teacher whose own actions have made it impossible for him to teach."</P>
<P>Of course, Al-Arian never did or said <I>anything</I> to deserve this injustice. But witch-hunting bigots like Jeb Bush don't care about the facts.</P>
