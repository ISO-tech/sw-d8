<P><font face="Times New Roman, Times, serif" size="5"><b>New York antiwar demonstration</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Andrew Jagunich</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 4, 2002 | Page 10</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK CITY--About 700 people rallied against the war at Rockefeller Plaza December 16. Suzanne Adely of Al-Awda and the Center to Indict Ariel Sharon spoke out against the mass arrests and detention of Arab Americans in the U.S.</P>
<P>"The men in our community are missing," she told the crowd. "It is your government's imperialism that is waging war against the Arab community here."</P>
<P>Union representative Dennis O'Neal spoke about how the government ordered postal workers to keep handling mail despite the presence of anthrax.</P>
<P>Afsaneh Moradian of New York Schools Against the War spoke about the student movement and the horrific conditions in Afghanistan. "Democracy is not starvation, democracy is not cluster bombs, democracy is not the Northern Alliance," she told the crowd.</P>

