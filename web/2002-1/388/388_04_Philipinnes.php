<P><font face="Times New Roman, Times, serif" size="5"><b>Philippines "terrorists" created by CIA</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 4, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>The 26 U.S. military advisers who were sent to the Philippines last year to "fight terrorism" will be targeting a group that the U.S. government helped to create.</P>
<P>According to various sources, Abu Sayyaf, the Islamic fundamentalist organization notorious for kidnapping tourists in southern Philippines and Malaysia, was formed and trained by the U.S. Central Intelligence Agency and the Philippine military.</P>
<P>Philippine Senator Aquilino Pimentel called for an inquiry into the link between the CIA and Abu Sayyaf--which he called a "CIA monster"--as early as May of last year. "There are now emerging bits of information that Abu Sayyaf was indeed the creation of probably the CIA in connivance with or with the support of some select military officers," he said at the time.</P>
<P>Meanwhile, the links between Abu Sayyaf and military and police authorities are well documented. In the recent book <I>Under the Crescent Moon: Rebellion in Mindanao,</I> journalists Marites Da&ntilde;guilan Vitug and Glenda Gloria document the bloody collaboration--which is also corroborated by former hostages.</P>
<P>Last September, a number of former hostages charged that Abu Sayyaf was a front--a "creation of the military's 'dirty tricks' department." They testified that army checkpoints would allow their captors to pass unmolested repeatedly.</P>
<P>This is the real story behind the talk of the "fight against terrorism" in the Philippines.</P>
<B><P>Eduardo Capulong,</B> San Francisco</P>

