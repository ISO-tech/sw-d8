<P><font face="Times New Roman, Times, serif" size="4">Mass uprising forces out the government</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Rebellion in Argentina</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 4, 2002 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>LEE SUSTAR reports on the mass uprising that toppled Argentina's president.</P></B>
<P>ONE WEEK after a mass uprising forced out Argentina's President Fernando de la R&uacute;a, the country's interim president quit amid new protests.</P>
<P>The situation was still unfolding as <I>Socialist Worker</I> went to press. But it's clear that the ouster of de la R&uacute;a and his hated economics chief Domingo Cavallo--followed in a week by the resignation of a second government--marks a tremendous victory for Argentine workers.</P>
<P>By taking on austerity plans pushed by the International Monetary Fund (IMF), the uprising is an inspiration to workers in every country where the IMF demands cutbacks.</P>
<P>Argentine workers, the unemployed and even the middle class have suffered through years of severe economic crisis. Unemployment surged to nearly 20 percent in recent months, and the government is behind on pension payments to 1.4 million retirees. Some 2,000 people fall below the poverty line <I>every day</I>.</P>
<P>The final straw came in mid-December, when de la R&uacute;a and Cavallo tried to push through more cuts to pay debts to Western banks and meet the IMF's conditions for new loans. After riots swept Buenos Aires, de la R&uacute;a declared a "state of siege"--opening the door for a crackdown by the military that ruled the country from the mid-1970s until 1983.</P>
<P>When protesters converged on the central Plaza de Mayo, de la R&uacute;a tried to hold on to power by brute force--with police firing on the crowd. But the demonstrations only increased in size.</P>
<P>Some elements from the opposition Justicialista Party, known as the Peronists, joined the demonstrations to boost their own credibility. But as the Argentine socialist organization MAS reported, "The motor of the revolt was the hunger to which this system has condemned millions of honest workers&#133;The repression was fierce. The images on TV provoked mass indignation. With the passing of hours, thousands of youths joined the combat, together with groups of the unemployed. The revolutionary left also threw itself into the mix."</P>
<P>Ultimately, de la R&uacute;a had to flee by helicopter to avoid the crowd.</P>
<P>Most Western press accounts focused on the fact that the increasingly impoverished middle class had joined the protests. But the uprising was the culmination of years of struggle by workers and the unemployed.</P>
<P>Shortly before de la R&uacute;a fled in disgrace, dissident union leader Hugo Moyano announced a general strike to force the government to resign--followed by a similar call by Rodolfo Daer, head of the big CGT union.</P>
<P>The Peronists, the largest party in the Argentine Congress, quickly moved to put themselves at the head of the struggle by voting Adolfo Rodriguez Saa as interim president until special elections could be held in March.</P>
<P>Rodriguez Saa suspended the repayment of Argentina's crushing debt and promised to create 1 million jobs. But in reality, the new president was doing his best to contain the struggle and shore up Argentina's political institutions.</P>
<P>He held a widely publicized meeting December 26 with Moyano and Daer to try to head off further strike action by unions. Rodriguez Saa promised to double the $200-a-month minimum wage and repeal laws that made it easier for companies to fire workers.</P>
<P>But three days later, another mass protest swept the capital--a show of anger at the new president for imposing a freeze on the hiring of government workers. Sweeping into Congress, demonstrators denounced corrupt politicians in Rodriguez Saa's new government.</P>
<P>Demonstrators were also angry that the Argentine Supreme Court maintained restrictions on their bank accounts that prevent people from accessing their pesos, which are worth one U.S. dollar each. Under Rodruigez Saa's plan, workers were to be paid in a new currency, the argentino, worth much less than the peso.</P>
<P>Meanwhile, the government would ensure that foreign investments and debts based in dollars were protected. As one protester's banner put it, "I put my money in the bank for them to look after it--not to be stolen."</P>
<P>Faced with a revolt among his own party's leadership, Rodriguez Saa quit.</P>
<P>The maneuvering of the Peronists shouldn't be surprising. The party controlled the government in the mid-1990s, under former President Carlos Menem, who imposed similar austerity policies. The Peronists' aim isn't to solve the burning issues of poverty and unemployment--but to get themselves back into office in the next elections.</P>
<P>But the continued protests show that working people in Argentina aren't prepared to settle for new faces presiding over the same policies. The protests and general strikes have shown that workers have the power to carry out more fundamental change--and set a fighting example for working people everywhere.</P>

