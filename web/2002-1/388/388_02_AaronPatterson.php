<P><font face="Times New Roman, Times, serif" size="5"><b>Judge hears death row torture case</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 4, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ILLINOIS DEATH row prisoner Aaron Patterson's struggle for justice will take center stage in a Chicago courtroom next week. Judge Michael Toomin is holding new hearings into allegations that Chicago police tortured Aaron--and fabricated a "confession" that was the main evidence used to convict him of the 1986 murder of an elderly South Chicago couple.</P>
<P>The Illinois Supreme Court ordered the hearings into the torture allegations and evidence that Aaron's original lawyer was incompetent.</P>
<P>Aaron has always maintained his innocence--and said that Chicago police tortured him. While in custody, Patterson used a paper clip to etch a message into a metal bench in the interrogation room.</P>
<P>The message, later photographed, read: "Aaron 4/30 I lie about murders. Police threaten me with violence. Slapped and suffocated me with plastic. No lawyer or dad. No phone. Signed false statement to murders."</P>
<P>The torture allegations aren't unique. Aaron is one of the Death Row 10, a group of death row prisoners tortured by Chicago cops. Pressure is building for Cook County State's Attorney Dick Devine to appoint a special investigator into the cases--and to agree to new trials.</P>
<P>In fact, Aaron's hearing came on the heels of an investigative series in the <I>Chicago Tribune</I> that exposed numerous cases of police and prosecutorial misconduct. Similar expos&eacute;s, along with years of organizing by death penalty opponents, pressured Illinois Gov. George Ryan to halt executions two years ago.</P>
<P>But the moratorium is under threat. Ryan isn't running for re-election this November, and most of the candidates to succeed him are pro-death penalty. What's more, the liberal Illinois Death Penalty Education Project has issued a report recommending procedural reforms to make the capital punishment system fairer.</P>
<P>This is a mistake. Cases like Aaron's show why the Illinois execution machine is broken--and can't be fixed.</P>
<P>Opponents of the death penalty should mobilize to defend the moratorium--and to demand that the death penalty be ended, once and for all.</P>

