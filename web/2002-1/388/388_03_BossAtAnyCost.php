<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Showing who's boss at any cost</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 4, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE GOVERNMENT of Afghanistan wants the U.S. to stop bombing. No, not the former Taliban government.</P>
<P>The new regime of Prime Minister Hamid Karzai--handpicked by the White House--last month demanded that the Pentagon end its bombing campaign after U.S. warplanes demolished a convoy of tribal leaders traveling to Kabul for Karzai's inauguration, killing some 60 people.</P>
<P>But Washington isn't backing down. Pentagon officials insist--against the word of survivors--that the convoy was packed with Taliban and al-Qaeda leaders.</P>
<P>So, apparently, was the remote village in eastern Afghanistan flattened by air strikes in the days that followed--at the cost of another 60 or so lives. And that's to say nothing of the 1.5 million Afghans who continue to face the threat of starvation this winter.</P>
<P>When it comes to killing Afghans, the U.S. government has been very successful. Yet the supposed goal of Washington's war--bringing Osama bin Laden and his al-Qaeda network to justice for the September 11 attacks in the U.S.--has failed so far. U.S. officials admit that they have no idea where he is.</P>
<P>Still, Bush and Co. haven't let this dampen their spirits. "Next year will be a war year as well," Bush rejoiced from his luxurious ranch last month.</P>
<P>The media were doing their part, too, scrambling to report connections between Richard Reid--who last month tried to explode a homemade bomb hidden in his shoe on a transatlantic flight to the U.S.--and the al-Qaeda network.</P>
<P>And the Pentagon is already picking new targets from Dick Cheney's list of "40 to 50" countries that could be next in the "war against terrorism." According to Britain's <I>Guardian</I> newspaper, U.S. military officials are hard at work on a plan for toppling Iraq's Saddam Hussein--with a campaign of saturation bombing and Special Forces ground operations backing up an offensive by the CIA stooges of the rebel Iraqi National Congress.</P>
<P>Meanwhile, a war that the U.S. didn't intend is threatening. Arch-rivals India and Pakistan--both partners in Bush's "coalition against terror"--were at the brink of a new conflict in early January.</P>
<P>Washington's war on Afghanistan set the stage for this confrontation.</P>
<P>India is only following Bush's example in issuing an ultimatum to Pakistan to "deal with terrorism or else." And the war in neighboring Afghanistan has stoked popular anger in Pakistan, putting the military regime in a tight spot.</P>
<P>U.S. politicians say that the cause of justice and peace was advanced by the carnage in Afghanistan. But the opposite is the case. Washington's war has added to the violence, instability and fear that plague the world.</P>
<P>And to what end? "The militarism that is there for all but the intellectually and morally impaired to see is the natural extension of the rapacious economic policies that have divided humanity as never before," antiwar journalist John Pilger wrote in Britain's <I>New Statesman</I> magazine.</P>
<P>Ultimately, Washington's war makers want to show who's boss--militarily and economically--around the globe. We have to stand up and oppose these war criminals.</P>

