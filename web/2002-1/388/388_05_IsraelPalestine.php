<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISRAEL/PALESTINE</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Israel holds Arafat hostage</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 4, 2002 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE ISRAELI government is holding Palestinian Authority (PA) chief Yasser Arafat a hostage in the West Bank city of Ramallah.</P>
<P>Arafat was barred from making his annual trip to Bethlehem for Christmas Eve mass on December 24. And as <I>Socialist Worker</I> went to press, Israeli officials declared their intent to keep him from attending a Greek Orthodox Christmas mass in Bethlehem on January 6.</P>
<P>The restrictions on Arafat's movements are Israel's latest attempts to humiliate him and further undermine his political influence. In early December, following three suicide bombings by Palestinians, the Israeli military destroyed Arafat's helicopters and the only airstrip in Palestinian-controlled territory. Arafat was stuck in Ramallah, under virtual house arrest.</P>
<P>But as the Israeli Defense Forces (IDF) lashed out at Palestinians day after day--and added to the hundreds dead and tens of thousands wounded in the latest stage of the violence--U.S. officials echoed the Israeli government's line that Arafat "must do more to stop terrorism."</P>
<P>The restrictions on Arafat's travel did draw some criticism at the end of December. But a dizzying array of curfews, blockades, checkpoints and hours-long waits have been routine for most Palestinians for 15 months.</P>
<P>"The IDF promises that 'humanitarian' cases are allowed through the checkpoints," journalist Amira Hass wrote in Israel's <I>Ha'aretz</I> newspaper. "If so, how come Tamer Kuzamer, a sick baby, and his mother, were not allowed through the Habla checkpoint to get to a doctor in Ramallah? His family looked for a roundabout way, much longer than the direct one, but the baby died en route&#133;"</P>
<P>"And why should a woman, who gave birth only 14 hours earlier, have to wait in an ambulance for hours at the exit from Nablus on the way back to a village only 10 minutes away by car?&#133;Every one of these examples should be multiplied by tens of thousands of people who are daily subjected to the same harm, in order to begin to understand the totality of the Israeli siege."</P>
<P>Israel's siege has also crippled the Palestinian economy, leaving 50 percent unemployed and 70 percent living on $2 a day or less.</P>
<P>"There's no doubt the result is collective pain on a very big scale," said a United Nations official. "It's peace-building in reverse."</P>
<P>Alongside its recent decision to declare Arafat "irrelevant," the Israeli government has stepped up the frequency and ferocity of its invasions into Palestinian territory--to carry out arrests, kidnappings and murders of alleged militants.</P>
<P>The brutality of Israeli military operations--combined with Israel's petty attempts to embarrass Arafat--have helped to forge unity between the secular and Islamist wings of the Palestinian resistance.</P>
<P>But there are also divisions. For example, the funeral of a 17-year-old boy killed by PA police in a Gaza refugee camp turned into a mass protest demanding freedom for Hamas and Islamic Jihad militants. When someone in the crowd fired on PA headquarters, police snipers fired back, killing six.</P>
<P>Every time Arafat caves to U.S. and Israeli demands and orders the arrest of Palestinian militants, he undermines his own position--which is precisely what the Israeli government wants. That's why the Israeli government will never be satisfied by Arafat's actions.</P>
<P>But without billions in aid from the U.S. each year, Israel's ability to terrorize Palestinians would be sharply diminished. Anyone who cares about justice must join the fight to end U.S. support for Israel's terror.</P>

