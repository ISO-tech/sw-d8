<P><font face="Times New Roman, Times, serif" size="5"><b>United mechanics ready to walk</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 4, 2002 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>JENNIFER BIDDLE, a member of International Association of Machinists (IAM) Local 1781 and a furloughed engine inspector at United Airlines, spoke to <I>Socialist Worker</I> about the recent strike authorization vote by IAM mechanics.</P></B>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><B><P>IAM MECHANICS voted 99 percent for a strike vote. Why was it so overwhelming?</P>
</B><P>THE MECHANICS are angry. We feel enough is enough. IAM members are the only employee group--union or non-union--not to have received raises or really even cost-of-living increases since 1994.</P>
<P>It is burning everyone up that our tax dollars are bailing out an industry that won't give us raises that we sacrificed for the past 10 years--ten years where they were making billions of dollars off our concessions, ten years of concessions during the biggest boom in the history of capitalism.</P>
<B><P>HOW HAS this affected your standard of living?</P>
</B><P>UNTIL THE past few years, most mechanics lived in the San Francisco Bay Area where the Maintenance Operations Center is.</P>
<P>For people like me--married with a child--it meant you couldn't afford to save to buy a house and essentially lived paycheck to paycheck. Others virtually live at United, working as much overtime as they can just to make ends meet.</P>
<P>It was surreal in the 1990s in the Bay Area to see some doing so well financially, and yet to feel yourself sink ever deeper into your own little fiscal crisis.</P>
<B><P>WHAT OTHER issues are behind the strike vote?</P>
</B><P>EVERY DAY the company seems to be bombarding us with new lies and contract violations.</P>
<P>The company laid off 16 percent of maintenance, and yet they are now asking us to work overtime. They're also outsourcing more of our work. Then there is the six-day workweek, which most people hate for obvious reasons.</P>
<P>There are also the little, everyday lies--our supervisors refusing to pay us for sick leave without a doctor's note&#133;supervisors not allowing shop stewards to conduct union business. Management's tactics have been so totalitarian, oppressive and hated that mechanics now refer to them as "the Taliban."</P>
<B><P>WHAT ARE the stakes for the labor movement in this fight?</P>
</B><P>THE STAKES are huge. Even our International is clear about this.</P>
<P>The union is pitching this as a fight against the Bush administration. They are saying that Bush wants this to be his PATCO.</P>
<P>Certainly Bush and United have a lot stacked in their favor. But so do we potentially. All the issues that have plagued the labor movement since PATCO are part of this struggle.</P>
<P>Unfortunately, IAM leadership is doing little [to prepare for the strike]. We need to have a very public, vocal campaign to win the support of the many workers in this country who can identify with what we are fighting for.</P>
<P>Most importantly, everyone I work with is looking forward to the day in February when we get to put our tools down and walk.</P>
<P>Ultimately, I believe what happens with our struggle--whether we win our strike or not--will shape the political and economic terrain for working people for years to come.</P>

