<P><font face="Times New Roman, Times, serif" size="5"><b>Protest David Horowitz</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Neal Ritchie</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 4, 2002 | Page 10</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHAPEL HILL, N.C.--"Blacks are unemployed because they are unemployable." One would expect this kind of racism from a grand dragon of the KKK, but it was actually said by David Horowitz, a well-known member of the Republican Party.</P>
<P>College Republicans invited Horowitz to speak at UNC Chapel Hill's campus November 29. Almost as soon as he reached the podium, he began insulting campus antiwar activists and berated the faculty and administration for allowing "anti-Americans" to carry out their activities.</P>
<P>But the International Socialist Organization, the Black Students' Movement, the Campaign to End the Death Penalty and a number of other student organizations had a plan. After 10 minutes, more than 100 students in the first 10 rows of the auditorium walked out. Coincidentally, Horowitz's microphone also malfunctioned, further interrupting his speech.</P>
<P>Although Horowitz mocked the demonstration and called it a "disgrace to the school's chancellor," participants saw the walkout as a total victory.</P>
<P>Thank goodness the "anti-Americans" refuse to be silenced.</P>

