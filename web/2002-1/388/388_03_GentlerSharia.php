<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>A kinder, gentler hanging</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 4, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE DEFEAT of the Taliban in Afghanistan is supposed to be a step forward for human rights.</P>
<P>The new Afghan government does say that it will continue to impose sharia law--the strict Islamist code that punishes crimes with executions and amputations. But it will be a kinder, gentler sharia.</P>
<P>"For example, the Taliban used to hang the victim's body in public for four days," Judge Ahahmat Ullha Zarif actually told the Agence France Presse news service. "We will only hang the body for a short time, say 15 minutes."</P>
<P>Zarif said adulterers would still be stoned, but "we will use only small stones." And unlike the Taliban, those who "confess" to their crimes would be given the chance to escape.</P>
<P>"If they are able to run away, they are free," said Zarif. "Those who refuse to confess their wrongdoing and are condemned by a judge will have their hands and feet bound so that they cannot run away. They will certainly be stoned to death."</P>

