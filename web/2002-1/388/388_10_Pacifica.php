<P><font face="Times New Roman, Times, serif" size="5"><b>Pacifica</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Petrino DiLeo and Lee Wengraf</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 4, 2002 | Page 10</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--A grassroots movement of listeners and activists is celebrating a recent win in its yearlong battle over control of the Pacifica Radio Network.</P>
<P>Several members of the Pacifica board who were pushing for greater corporate control of the progressive radio network will step down and be replaced by a listener-backed slate. The new board also voted December 29 to return <I>Democracy Now!</I> and host Amy Goodman to the airwaves.</P>
<P>Several Pacifica programmers who led the so-called "Christmas coup" of 2000, which locked out progressive employees at New York's WBAI station, have also been forced to resign.</P>
<P>Under the new agreement, the reform movement will most likely have a nine-to-six majority on a 15-month interim board, during which time the sale of any station is forbidden.</P>
<P>The struggle still has some hurdles to overcome. Directors and programmers from WBAI who were fired have yet to be reinstated, and the gag rule banning on-air discussion of the battle within Pacifica has not been lifted.</P>
<P>Additionally, Goodman has yet to settle outstanding grievances, and unionized stringers are still on strike. "We're feeling very positive, but the battle must still be fought politically, through a grassroots campaign," union activist and new Interim Board Member Ray LaForest told <I>Socialist Worker</I>.</P>
<I><P>The next meeting for Pacifica supporters will be January 8 at 6 p.m. at WBAI, 120 Wall Street, New York. For more information, go to www.pacificacampaign.org.</P>
