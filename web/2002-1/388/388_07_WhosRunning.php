<p><a name="CHENEY"></a><font face="Times New Roman, Times, serif" size="3"><b>DICK CHENEY</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">VICE PRESIDENT Dick Cheney has kept a low profile during the U.S. war on Afghanistan. But don't think that he's out of the loop. By most accounts, Cheney has been pulling the strings since day one of President Moron's reign.</P>
<P>He's about as right wing as they come in Washington. As a member of Congress from Wyoming during the 1980s, Cheney racked up a right-wing record to rival the likes of Jesse Helms.</P>
<P>He was an opponent of "terrorism" back then, too--only he was fond of denouncing Nelson Mandela and his African National Congress as the world's main "terrorist" threat.</P>
<P>Cheney moved over to the Pentagon to become Papa Bush's defense secretary in 1989, where he helped to run the Gulf War.</P>
<P>When Bush was run out of Washington in 1992, Cheney followed his boss into the world of big oil. He became filthy rich as CEO of Halliburton, the world's largest oil services company.</P>
<P>No wonder Cheney took a hands-on approach to energy policy as vice president, organizing a special White House task force to "study" the issue, which he packed with old cronies from the oil industry. No one knows exactly who, though--because Cheney refused to disclose the names of task force members, even after Congressional investigators demanded the information.</P>
<P>Now Cheney is part of the hunt to find a new enemy in the "war against terrorism." Who's on his short list? Some "40 to 50" countries, stretching from North Korea and the Philippines in East Asia to Colombia on the other side of the globe.</P>

