<p><a name="RUMSFELD"></a><font face="Times New Roman, Times, serif" size="3"><b>DONALD RUMSFELD</b></font></P>
<p><font face="Times New Roman, Times, serif" size="3">IF YOU wanted to pick one person as a symbol of the connection between the Washington elite and Corporate America, Defense Secretary Donald Rumsfeld would be your man. Rumsfeld has spent decades shuttling between the corridors of political power and corporate boardrooms.</P>
<P>He rose to prominence in the Nixon and Ford administrations, becoming defense secretary in 1975. Presiding over the rehabilitation of the Pentagon following the U.S. defeat in the Vietnam War, "Rummy" laid the groundwork for a range of new weapons systems.</P>
<P>When the Republicans lost the White House in 1976, Rumsfeld started out his new corporate career at the top, taking over at drug giant G.D. Searle and later technology firm General Instrument.</P>
<P>As a CEO, the man who never met a proposed Pentagon weapons system he didn't like suddenly discovered the virtues of "cost-cutting"--and slashed 30 percent of the workforce at both companies.</P>
<P>Returning to his old job under Dubya, Rumsfeld's main role before September 11 was as the administration's most fanatical supporter of a Star Wars missile defense system--not to mention killer satellites, laser ray guns and assorted other space weapons.</P>
<P>Since the war on Afghanistan began, Rumsfeld has hogged the limelight with his daily press briefings--where his open contempt for all questions about military operations competes with his delight in detailing every barbaric aspect of the war.</P>
<P>Now, Rumsfeld is ready for more--rejecting his advisers' proposals for new countries to target after Afghanistan as "not radical enough."</P>

