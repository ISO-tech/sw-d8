<p><a name="ASHCROFT"></a><font face="Times New Roman, Times, serif" size="3"><b>JOHN ASHCROFT</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">IS THERE anyone who cares less about your rights than Attorney General John Ashcroft?</P>
<P>Civil rights? Ashcroft built his political career in Missouri "out of opposing school desegregation in St. Louis and opposing African Americans for public office," wrote the <I>St. Louis Post Dispatch</I>.</P>
<P>A women's right to choose? As a senator, Ashcroft was one of three cosponsors of the Human Life Amendment, which would not only outlaw abortions but some forms of contraception.</P>
<P>Rights of defendants? Not if they get in the way of police and prosecutors fighting the "war on drugs."</P>
<P>Defeated for reelection to the U.S. Senate in 2000 by a dead man, Ashcroft was bumped upstairs by Dubya. Though his nomination was widely opposed, Senate Democrats allowed Ashcroft to squeak through confirmation with his promises to "respect the Constitution."</P>
<P>But since September 11, Ashcroft has violated it, amendment by amendment. His Justice Department has detained more than 1,000 people in connection with the investigation into the air attacks. Yet America's chief witch-hunter can't say that a single person has been charged with a crime directly related to the September 11 attacks.</P>
<P>Still, Ashcroft isn't apologizing. When senators finally got him to explain himself in early December, the sullen attorney general denounced his critics as no better than terrorists themselves. "[T]o those who scare peace-loving people with phantoms of lost liberty," Ashcroft said, "my message is this: Your tactics only aid terrorists, for they erode our national unity and diminish our resolve."</P>

