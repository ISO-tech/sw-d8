<p><a name="POWELL"></a><font face="Times New Roman, Times, serif" size="3"><b>COLIN POWELL</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">THE MAINSTREAM media have painted Secretary of State Colin Powell as the administration's "dove," fighting against "hawks" like Donald Rumsfeld for a more moderate war. Straw-grasping liberals latched onto the idea as well, hoping to discover the champion of a kinder, gentler "war against terrorism."</P>
<P>But when it comes to committing war crimes, Powell is second to none among the Bush gang. He has the experience.</P>
<P>As chair of the Joint Chiefs of Staff under Papa Bush, Powell presided over the 1991 Gulf War against Iraq. An estimated 200,000 Iraqis, most of them civilians, were killed in Operation Desert Slaughter's saturation bombing campaign. Powell followed up the air strikes with a blitzkrieg ground offensive in which surrendering Iraqi soldiers were slaughtered by the thousands.</P>
<P>The media claim that Powell's primary concern has been "coalition-building" and winning an "international consensus" for U.S. military adventures. But Powell made his priorities clear after the 1989 U.S. invasion of Panama: "We have to put a shingle outside our door saying 'superpower lives here.'"</P>
<P>When Powell was nominated as Bush Jr.'s secretary of state, he was greeted as a rock of integrity. But this "man of principle" didn't even flinch in wrecking the United Nations World Conference Against Racism in Durban, South Africa, last summer--after delegates dared to raise criticisms of Israel.</P>
<P>If Powell isn't always reading from the same page as Rumsfeld and Co., you can be sure that they're all using the same book.</P>

