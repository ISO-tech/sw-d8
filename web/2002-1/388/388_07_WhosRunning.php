<p><font face="Times New Roman, Times, serif" size="5"><b>Who's running the war?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 4, 2002 | Page 7</font></p>

<P><font face="Times New Roman, Times, serif" size="3"><i>SOCIALIST WORKER'S</i> guide to the chief war makers in Washington.</P>

