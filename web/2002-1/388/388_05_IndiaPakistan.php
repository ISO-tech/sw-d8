<P><font face="Arial, Helvetica, sans-serif" size="2"><b>INDIA/PAKISTAN</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Stepped-up skirmishes along the border</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Nuclear rivals at the brink of war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By David Whitehouse</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 4, 2002 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NUCLEAR RIVALS India and Pakistan were on the edge of a nightmarish war at the turn of the new year. As <I>Socialist Worker</I> went to press, India continued a massive troop buildup along its 1,800-mile border with Pakistan, whose military regime responded with new deployments of its own.</P>
<P>The epicenter of the crisis is the disputed state of Kashmir, where the Indian government evacuated some 20,000 people who live near the Line of Control that separates the Indian- and Pakistani-occupied areas.</P>
<P>The standoff--which could easily escalate into the fifth war between the two rivals in five decades--follows a December 13 suicide attack on the Indian parliament building in New Delhi. The Indian government blames the attack on two Pakistani-based Islamist groups that want Indian-occupied Kashmir to become part of Pakistan.</P>
<P>Claiming that Pakistan's Interservices Intelligence agency (ISI) assisted the attackers, India--along with the U.S.--is pressuring Pakistan's military ruler, Gen. Pervez Musharraf, to crack down on Islamic militants.</P>
<P>No group has claimed responsibility for the attack, in which seven Indians and all five attackers were killed. But India--using evidence that it refuses to share with Pakistan--blames the groups Lashkar-e-Taiba and Jaish-e-Muhammad.</P>
<P>Jaish-e-Muhammad was responsible for an October bomb attack on the Kashmir state assembly in Srinagar. The group has close ties to Afghanistan's Taliban--through the party that gave political and religious training to both, Pakistan's Jamiat-ul-Ulema-e-Islam.</P>
<P>ISI agents did help Jaish and Lashkar get off the ground in the 1990s. But the increasing independence of both groups has alarmed Musharraf and other leaders of Pakistan's most important institution--the army.</P>
<P>In the last week of December, Musharraf cracked down by freezing financial assets of the two groups and arresting leading members. With these moves, Musharraf stepped up a crackdown that began when he signed up with the U.S. "war against terror" in September.</P>
<P>In fact, the U.S. war is the major factor driving India and Pakistan toward a new conflict. The attacks in Srinagar and New Delhi, though directed against India, were calculated to force Musharraf to break off his new relationship with the U.S. and take a harder stand in favor of the anti-Indian struggle in Kashmir.</P>
<P>What's more, U.S. war rhetoric has given cover for India's far-right government--led by Prime Minister Atal Behari Vajpayee--to launch an assault on "terrorists and the state that harbors them."</P>
<P>Musharraf is using the repression to solidify his own position in Pakistan. But he has to avoid appearing to cave in to Indian and U.S. pressure, especially on the widely popular struggle for self-determination in Kashmir.</P>
<P>Given these conflicting pressures, it's unclear how much more he will--or can--do to satisfy India's demands. Yet Vajpayee can't back off until he has a clear victory to show for it. That's why the threat of a war--between two nuclear powers--looms over the new year.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Roots of the conflict in Kashmir</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WHEN BRITISH colonialists left South Asia in 1947, their former colony split into two states--Hindu-dominated India and Muslim-dominated Pakistan. But India kept control of the state of Kashmir, which is 80 percent Muslim--an ongoing source of conflict to this day.</P>
<P>From the beginning, India routinely fixed elections in Kashmir to favor parties that wanted a closer relationship with New Delhi--while discriminating against Muslims in federal jobs and the judicial system.</P>
<P>The Indian government promised autonomy again and again, but withheld it--and instead based thousands of troops in Kashmir, as both a defense against Pakistan and insurance against a local uprising.</P>
<P>Discontent reached a boiling point in 1989 after another fixed election. Kashmiri workers took to the streets in mass strikes and demonstrations.</P>
<P>In the early, mass phase of the struggle, secular nationalist ideas dominated, and few people called for joining Pakistan. But when India crushed the movement--with 500,000 troops that still occupy the state--the defeat of secular nationalism opened the way for Islamist militants, including many based in Pakistan, who were ready to carry on underground warfare.</P>
<P>Since the early 1990s, Indian repression has killed 70,000 Kashmiris, mostly civilians. The occupiers regularly torture and murder detainees.</P>
<P>The Islamists don't have wide support in Kashmir. One reason is that most Kashmiris adhere to a unique brand of Sufi Islam, which is closer to Shiism than to the Sunni version that dominates in Pakistan.</P>
<P>The Indian government is hoping that an aggressive stand will further isolate the Islamists. But after a decade of brutal repression, it may have trouble finding friends in Kashmir.</P>

