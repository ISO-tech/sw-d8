<P><font face="Times New Roman, Times, serif" size="5"><b>Ali good, but not "The Greatest"</b></font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>MOVIES: <I>Ali,</I> </B>directed by Michael Mann, written by Eric Roth and Michael Mann, starring Will Smith, Jon Voight, Jamie Foxx and Mario van Peebles.</P></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 4, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>DIRECTOR MICHAEL Mann's film <I>Ali</I> could have been a whitewash of the famous boxer's life, ending up along the lines of <I>Rocky</I>. But instead, <I>Ali</I> does a good job of showing both the highs and lows of the career of Muhammad Ali (Will Smith).</P>
<P>Covering a 10-year period (1964-74), the movie celebrates Ali's determination to be the champ in his own way, without sacrificing his political principles, even while being sacrificed on the altar of public opinion for his membership in the Nation of Islam and his refusal to fight in the Vietnam War.</P>
<P>In one of the movie's best scenes, Malcolm X (Mario van Peebles) visits Ali in a New York City hotel room overlooking the Apollo Theater. Ali recounts as a child seeing a picture of Emmett Till, a Black boy who was lynched for looking at a white woman, and describes the mark it made on him.</P>
<P>Malcolm, who was by this time estranged from the Nation of Islam, describes, following the 1963 bombing of a Birmingham, Ala., church, feeling paralyzed with rage and grief--and having no way to express it. Since Birmingham was the "territory" of Martin Luther King and the mainstream civil rights movement, the Nation of Islam told its members to keep a distance.</P>
<P>The climax of the movie, the 1974 "Rumble in the Jungle," in which Ali fought George Forman in Kinshasa, Zaire, is spectacularly staged. Promoter Don King and President Mobutu Sese Seko's cynical exploitation of the fight are contrasted to Ali's desire for the fight to promote Black Power. Ali is shown as a hero to the people of Zaire, precisely because of the radical politics for which he is vilified in the U.S.</P>
<P>Unfortunately, the film has some problems. It is often short on insight into what drove Ali to make the choices that he did, which means that some key events are not clearly explained.</P>
<P>But the biggest problem is that Ali's bravado and charisma are sometimes absent. Although the words are always there, Smith's delivery feels flat in early scenes.</P>
<P>However, there are moments when Smith's performance is brilliant, as in the scene depicting Ali's refusal to fight in Vietnam--despite the fact that he was threatened with five years in prison, stripped of his title and lost three of the most important years of his career.</P>
<P>These moments make the film worth watching. And they are particularly welcome, given Ali's recent decision to appear in U.S. public service announcements for Arab TV to "explain" the current war in Afghanistan to Muslims.</P>
<P>That Ali could be associated with a project to sell the war is discouraging. But it also shows how the same establishment figures that railed against him in the 1960s and 1970s today embrace him as a symbol of "American unity."</P>
<P>That's why the depiction of the champ's unflinching stand against the Vietnam War and his refusal to be broken by the political backlash make <I>Ali</I> a welcome, if not great, film.</P>

