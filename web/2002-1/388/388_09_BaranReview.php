<P><font face="Times New Roman, Times, serif" size="5"><b>The struggle of refugees in Iran</b></font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>MOVIES: <I>Baran</B>,</I> written and directed by Majid Majidi. Opens for a second run in New York and Los Angeles in February.</P></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Meneejeh Moradian</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 4, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><I><P>BARAN</I>--meaning "the rain," in Farsi--is a close-up look at displaced peasants who are forced to travel to Iranian cities for work. There, they compete with an ever-increasing pool of cheap labor, including ethnic Turks and--the least fortunate of all--Afghan refugees.</P>
<P>Like undocumented immigrants everywhere, Afghans face government persecution and are forced to take the worst jobs because they cannot work legally.</P>
<P>Set in the mid-1990s, most of the action takes place on a desolate construction site where the harsh Iranian winter combines with backbreaking labor to create a bleak stage where the characters struggle to survive.</P>
<P>Lateef, a young ethnic Turk with Iranian citizenship, has a relatively easy job serving tea and meals to the workers. When Najef, an Afghan, falls and breaks his foot, Lateef loses his cushy job to Najef's younger, weaker son, Rahmat. However, Lateef's resentment soon turns to solidarity after he discovers Rahmat's secret and falls in love.</P>
<P>This film is moving in the way it highlights the humanity of those who have been abandoned by society. Lateef is moved to incredible acts of kindness, which can't help but fall short of the kind of economic help that is necessary to lift the heavy burden of poverty.</P>
<I><P>Baran </I>is a disturbing and courageous story that sheds light on the everyday acts of heroism that usually go untold.</P>

