<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Argentina's uprising shows alternative to market misery</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The power of revolt</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 4, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE UPRISING that toppled Argentina's President Fernando de la R&uacute;a last month, and then, a week later, his interim successor, was an inspiring demonstration of the power of mass struggle and showed the potential for an alternative to a system wracked by economic crisis and war.</P>
<P>The immediate cause of de la R&uacute;a's resignation on December 20 was massive food riots by the unemployed. But this was the culmination of a struggle that has been growing throughout an economic crisis that began more than three years ago.</P>
<P>A week before de la R&uacute;a fell, Argentina saw its seventh general strike in two years. For months before that, organizations of the unemployed in cities and towns across the country mobilized to demand emergency aid. In a few towns, these groups essentially took over the running of local affairs.</P>
<P>When de la R&uacute;a froze bank accounts in December to head off a spiraling financial crisis, the struggle exploded to a new level.</P>
<P>The government organized a savage crackdown that left more than two dozen dead. But the repression couldn't stop mass protests. De la R&uacute;a and his hated economics chief, Domingo Cavallo, were forced to resign.</P>
<P>U.S. officials promised to help the new government deal with the crisis. But Argentinians know that Cavallo--who came to prominence under the military dictatorship of the 1970s and early 1980s--was Washington's man.</P>
<P>It was his proposal a decade ago to create a permanent one-to-one relationship between the Argentine currency, the peso, and the U.S. dollar--which helped to drive the economy into recession three years ago.</P>
<P>At the same time, Argentina followed the free-market policies demanded by the International Monetary Fund--deregulation of business, privatization of state-owned companies and "flexible" labor policies. From one of the most advanced societies in Latin America, Argentina was plunged into poverty. Yet the government's only response was more cutbacks.</P>
<P>Last month, ordinary Argentinians rose up and said "no."</P>
<P>The struggle was still unfolding as <I>Socialist Worker</I> went to press. In fact, the interim president who took over from de la R&uacute;a had already resigned after new protests at the end of December.</P>
<P>Now, however, Argentine workers and the poor face new challenges. One of them is confronting the Peronists--the biggest political party in the Argentine Congress--which quietly encouraged the December demonstrations to get rid of de la R&uacute;a and take charge themselves.</P>
<P>The Peronists want to balance between offering concessions and protecting the interests of Argentine and multinational corporations. This fits a pattern seen around the world in recent years following mass uprisings that toppled unpopular governments.</P>
<P>From Eastern Europe in 1989, to Indonesia in 1998, to Serbia and the Philippines in 2000, demonstrations of "people power" overthrew even highly repressive regimes. But these "people power" movements united different forces with conflicting interests.</P>
<P>The mass of workers and the poor have an interest in fundamentally changing the system. But other elements of these movements want only to reorganize the state machine and leave the social structure basically unchanged.</P>
<P>In Argentina, the Peronists may offer different policies than de la R&uacute;a. But their aim will be the same--to solve the economic crisis on the backs of Argentine workers.</P>
<P>Yet the struggle itself has pointed the way to an alternative. The workers whose general strikes shut down many of Argentina's factories and offices can use their power to take control of them. And the committees of the unemployed that blocked highways can organize emergency distribution of food and other services.</P>
<P>The working class of Argentina has the opportunity to lead a struggle to transform the system--and replace it with one that meets human needs. But this will mean continuing to confront those who try to stop the struggle halfway</P>

