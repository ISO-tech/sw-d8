<P><font face="Times New Roman, Times, serif" size="5"><b>Sneaking off to Bermuda to save on taxes</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 12, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>In its effort to sell the public on a permanent state of war, the Bush administration has called upon Americans to "sacrifice" in the name of patriotism. Too bad that call doesn't extend to large corporations.</P>
<P>In a recent article in the <I>New York Times, </I>David Cay Johnston reported on how corporate giants have used tax loopholes to keep their hands on millions of dollars. By reincorporating in Bermuda, these companies have deemed themselves foreign corporations--and therefore pay taxes only on whatever profits they characterize as American.</P>
<P>The move "overseas" is purely fictitious. For example, the chief financial officer of Ingersoll-Rand admitted that the company wouldn't even set up an office in Bermuda--but would merely pay a service organization to accept mail. </P>
<P>All the while, these newly born "foreign" corporations enjoy all the benefits of being located in the U.S. As syndicated columnist Mark Shields wrote, "They and their property are protected by the American legal system. Their contracts are enforced by American courts." Such shenanigans allowed the New Hampshire-based toymaker Tyco to cheat the government out of $400 million last year.</P>
<P>Even Kay Barton of Ernst &amp; Young, the accounting firm aggressively promoting the scheme, concedes that such tax avoidance might appear unpatriotic. But, as she told a reporter, "The improvement on earnings is powerful enough so that maybe the patriotism issue needs to take a backseat."</P>
<P>And many in the Bush administration advocate expanding these foreign loopholes--allowing companies to wrap themselves in the flag, while at the same time avoiding paying their fair share.</P>
<B><P>Chuck Augello,</B> Hillsborough, N.J.</P>

