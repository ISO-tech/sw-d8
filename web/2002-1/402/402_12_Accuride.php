<P><font face="Times New Roman, Times, serif" size="5"><b>Abandoned by the UAW at Accuride</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 12, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IT TOOK just 19 words for the United Auto Workers (UAW) to abandon more than 400 workers in a local with a 23-year history of struggle--the last four years in a brutal lockout. "The International Union and its Local 2036 hereby disclaim interest in representing hourly employees at Accuride's Henderson, Kentucky facility," wrote Terry Thurman, director of UAW Region 3, in a letter dated March 28.</P>
<P>The letter wasn't sent to Local 2036, however. UAW officials were too cowardly for that. They wrote directly to Nate Niemuth, the union-busting Arizona attorney who runs labor relations for Accuride.</P>
<P>"The reaction here is total disgust," said Billy Robinson, former president of Local 2036. "People can't believe that they would do this. This was [UAW President Stephen] Yokich cleaning things up for Ron Gettelfinger," his handpicked successor to be elected at the UAW's convention in June.</P>
<P>It was Gettelfinger, then the director of UAW Region 3, who instructed Local 2036 to strike the truck wheel-making company in February 1998. Workers offered to return to work unconditionally one month later. But Niemuth--who busted the copper miners' union at Phelps Dodge in the 1980s--locked them out. Each management offer got worse, essentially stripping the union of all rights.</P>
<P>In August 1999, the UAW International Executive Board (IEB) cut off strike pay and medical insurance to try to force Accuride workers into surrendering to Niemuth. Instead, Robinson and Local 2036 members stepped up their solidarity campaign--including a picket of the UAW's headquarters in Detroit in May 2000.</P>
<P>An embarrassed IEB restored benefits after 14 months. But the damage had been done to locked-out workers who suffered ill health from using the highly toxic chemicals at the plant for years.</P>
<P>In January, the IEB moved to cut off benefits again--this time for good. But Local 2036 remained defiant. They voted to reject--as they had many times in the past--the union-busting offer from management.</P>
<P>In the end, the UAW International did what Niemuth and Accuride couldn't do--destroy Local 2036. The cutoff of benefits will almost certainly lead to the deaths of a number of Local 2036 members--including some who had been at Accuride since winning a strike for their first union contract in 1980.</P>
<P>"We have a member, John Nall, who had a stroke just after benefits were cut off," Robinson said. "He's got 98 percent blockage on the left side of his neck. They can't operate. He's just 48 years old--I worked with him for 20 years. Did stress cause this? There's no doubt in my mind about it. He didn't have anything--no job, no prospect of a job."</P>
<P>The struggle at Accuride should have been an all-out battle for the UAW. Since the factory is a major supplier to important Ford, General Motors and Navistar plants, the union had the leverage it needed to rebuild in the parts industry, which is now 80 percent nonunion.</P>
<P>Instead, the war at Accuride laid bare the bureaucratic rot in the UAW and exposed the self-serving careerists who run the union.</P>
<P>But the battle at Accuride also brought the finest traditions of the UAW and the labor movement to the fore--solidarity, democracy and struggle. That is why workers everywhere owe the members of Local 2036 an enormous debt. They have given the entire labor movement an example of the determination that rank-and-file workers will need to take their unions back.</P>
<P>"We will stay here till hell freezes over, with or without the UAW," Robinson said. "We won't give up."</P>
