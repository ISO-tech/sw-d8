<p><font face="Arial, Helvetica, sans-serif" size="5"><b>What we demand</b></font></P>
<STRONG><font face="Times New Roman, Times, serif" size="3"><P>THE TEAMSTERS are demanding 3,000 new full-time jobs per year. We also need:</P></STRONG>
<P>--$12 start pay for part-timers <BR>
--Equal pay for equal work <BR>
--Full benefits after 40 days <BR>
--Job security and protection against layoffs</P></td>
