<P>The Teamsters are demanding a three-year contract with 3,000 new full-time jobs created nationally each year. Although wage issues have yet to be proposed by the union or the company, workers in Chicago want part-time wages to be raised to $12 an hour from $8.50.</P>
<P>We need to stay focused on UPS--to press the fight that we have made UPS the world's largest transportation company and demand a good contract. And if that means we must be willing to strike to get it, so be it.</P>
<P>That also means we must be united in our struggle. But at a recent Local 705 meeting, some members made disgustingly racist comments when it was proposed to have our contracts made available in Spanish and Polish.</P>
<P>And it was cowardly that Local 705 leaders, like self-described progressive Secretary-Treasurer Jerry Zero, didn't speak out against this stupid show of English-only bigotry that has poisoned the union movement far too long in this country. This stupidity only plays into the hands of management's divide-and-conquer strategy.</P>
<P>Meanwhile, management is trying to undermine support for strike action by whining about FedEx's nonunion workforce. Management wants us to sign a contract early, or otherwise they claim FedEx will poach business from UPS.</P>
<P>But unless UPS gives in to our just demands, we must be prepared to strike this blood-sucking company to get our fair share of the record-breaking profits.</P>

