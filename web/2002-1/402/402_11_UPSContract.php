<P><font face="Times New Roman, Times, serif" size="4">UPS pleads poverty despite strong performance</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"We've got a fight at UPS"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Donny Schraffenberger, steward, Teamsters Local 705</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 12, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CONTRACT NEGOTIATIONS between United Parcel Service and the Teamsters are heating up.</P>
<P>UPS has had five record profit years since the last contract--and management predicts that it expects to meet or exceed its long-term historical performance levels in 2003 and 2004.</P>
<P>Also, UPS has recently built a new hub in the Philippines, is growing rapidly in Europe, and its nonunion logistics section, UPS Logistics, is the pride of management. UPS stock is up almost 20 percent from September 17, 2001--and JP Morgan Chase has raised its rating on UPS to "buy" from "long-term buy."</P>
<P>But at UPS, management is trying to paint the picture to union workers that the company is under siege from the U.S. Post Office and FedEx. They want us to think of the "interest of the company" and settle the contract in an admirable fashion, i.e., to keep the status quo.</P>

