<P><font face="Times New Roman, Times, serif" size="4">Man beaten for the "crime" of being Pakistani</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Cops that tortured Louima strike again</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 12, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>Attorney General John Ashcroft's racist scapegoating of immigrants has given renewed confidence to racist police from the same precinct where Haitian immigrant Abner Louima was tortured in 1997. </P>
<P>On March 11, 70th Precinct cops beat a 48-year-old Pakistani man in Brooklyn. Raja Aftab Iqbal was trying to cross the street to get to his job at the same time that police were intervening in an unrelated street fight. </P>
<P>After telling Iqbal he wasn't allowed to cross the street, one officer reportedly called Iqbal "Taliban." Iqbal told <I>Newsday,</I> "That's the only word he said, and he hit me in my face and after that I don't know how many people were hitting me--six or eight. They arrested me, put handcuffs on me and they still beat me."</P>
<P>Iqbal was taken to the 70th Precinct station house--the same one where Abner Louima was tortured in 1997--where he was charged with "disorderly conduct" after being detained for over an hour.</P>
<P>To many Pakistani immigrants, the attack is an expression of the heightened racism they have endured over the last six months. Asghar Choudhri, president of the Pakistani American Federation of New York, told a reporter, "Before September 11, they blamed Afro-Americans for everything. Now they blame everybody of Middle Eastern origin, or if you have a beard, they call you 'al-Qaeda.'"</P>
<P>Pakistani community leaders, immigrant rights groups and a coalition organizing to stop the secret detention of immigrants are planning a response to this vicious attack.</P>
<B><P>Tristin Adie,</B> New York City</P>

