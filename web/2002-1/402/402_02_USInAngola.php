<P><font face="Times New Roman, Times, serif" size="4">They fueled a savage war</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>U.S. operation in Angola exposed</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 12, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>RECENTLY DECLASSIFIED documents have exposed the U.S. government's bloody role in the decades-long civil war in the African country of Angola.</P>
<P>More than half a million Angolans were killed and an estimated 4 million--nearly one-third of the country's population--were forced to flee their homes during the years of war after Angola won independence from Portugal in 1975.</P>
<P>The U.S. government led the way in pouring fuel on the fire. It spent millions to support UNITA "rebels" against the new government led by the Popular Movement for the Liberation of Angola (MPLA)--which was seen as "communist" because it received limited support from Cuba and the former USSR.</P>
<P>The U.S. has long admitted that it sponsored covert operations in Angola, but always claimed that it was responding to an airlift of Cuban soldiers into the country to support the MPLA. But U.S. documents released last month show the truth--that the Ford administration intervened in Angola weeks before Cuban forces landed.</P>
<P>"When the United States decided to launch the covert intervention, in June and July, not only were there no Cubans in Angola, but the U.S. government and the CIA were not even thinking about any Cuban presence in Angola," said Piero Gleijeses, a professor at Johns Hopkins University who used the Freedom of Information Act to uncover the documents. "If you look at the CIA reports which were done at the time, the Cubans were totally out of the picture," Gleijeses told <I>the New York Times.</P>
</I><P>Eventually, Cuba did send troops in support of the MPLA, which held the capital in the months just before independence from Portugal was declared in August 1975. But Gleijeses' research shows that Cuba intervened only after a CIA-financed covert invasion through neighboring Zaire (now the Congo)--and apartheid South Africa's simultaneous attack on the capital.</P>
<P>Even former U.S. officials admitted that Gleijeses' findings were true. "Although we desperately wanted to find Cubans under every bush," said Robert Hultslander, the former CIA station chief in Angola, "during my tenure, their presence was invisible, and undoubtedly limited to a few advisers."</P>

