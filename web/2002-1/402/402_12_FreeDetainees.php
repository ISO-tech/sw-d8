<font face="Times New Roman, Times, serif" size="5"><b>Being Arab is not a crime</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 12, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ANSAR MAHMOOD thought that he was living the American dream. In 1999, Mahmood, who is from Pakistan, was awarded a green card in the Immigration and Naturalization Service's lottery system.</P>
<P>He arrived in the U.S. and settled in Hudson, N.Y., where he took a job delivering pizzas--and worked 13-hour shifts in order to send money home to his family to pay for his sisters' educations and for medicine for his father's heart condition.</P>
<P>In October, Mahmood decided to send pictures of his new hometown. When he asked a security guard to take a photo of him overlooking a local water treatment plant, the guards became suspicious and called police. That's when Mahmood's "American dream" turned into a nightmare.</P>
<P>Two days later, police investigators discovered that Mahmood had paid for the first month's rent and car insurance for a Pakistani couple living in the area illegally. Mahmood was arrested in January for "harboring an illegal immigrant." And now the government is deporting him.</P>
<P>He's just one of the approximately 2,000 immigrants who were detained in the aftermath of September 11. Not a single one was found to have any connection to the attacks, but that didn't stop the Justice Department from victimizing many for minor visa or other violations. And if the Bush gang gets its way, there will be plenty more scapegoats in the coming months.</P>
<P>Not satisfied with its earlier round of racial profiling--interviews of 5,000 mostly young Arab men that began last November--the Justice Department announced in March that it plans to go after another 3,000 immigrants for the same treatment.</P>
<P>Attorney General John Ashcroft actually claimed that these interviews help Arab and Muslim immigrants feel "welcome." "[T]he process of reaching out to foreign nationals and their communities fostered new trust between law enforcement and these communities," Ashcroft said.</P>
<P>Maybe Ashcroft thinks that the Justice Department's continued raids on Muslim charities are also fostering "new trust." He should ask Aysha Unus.</P>
<P>On March 20, federal agents burst into Aysha's home with their guns drawn as part of a raid on 16 Muslim charities and residences in northern Virginia and Georgia. Aysha and her 19-year-old daughter were handcuffed for five hours while agents rifled through their home. "We feel that the system humiliated us," Aysha told the <I>Washington Post</I>.</P>
<P>Meanwhile, on April 6, a federal judge ruled that the Justice Department could use "secret evidence" to keep the Global Relief Foundation, a Muslim charity, shut down. "They've tied our hands behind our backs and blindfolded us," Global Relief's lawyer Roger Simmons told the <I>Chicago Tribune</I>.</P>
<P>But even this isn't enough for the Bush gang. The Justice Department announced that it was appealing a ruling by another judge that opened the trial of Global Relief's cofounder Rabih Haddad to the public. That means Haddad--who has been sitting in jail for months without being charged--could be deported without ever being able to defend himself.</P>
<P>These attacks on people guilty of no other "crime" than being Arab would be beyond belief--if they weren't so terribly real. We have to unite to stop this attack on Arabs and Muslims.</P>

