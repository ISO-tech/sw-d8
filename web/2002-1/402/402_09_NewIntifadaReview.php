<P><font face="Times New Roman, Times, serif" size="5"><b>Roots of the new Palestinian Intifada</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 12, 2002 | Page 9</font></p>

<font face="Arial, Helvetica, sans-serif" size="2"><B><P>BOOKS:</B> Edited by Roane Carey, <B><I>The New Intifada: Resisting Israel's Apartheid</I>.</B> Verso, 2001, 354 pages, $20.</P></font>

<font face="Times New Roman, Times, serif" size="3"><B><P>ERIC RUDER reviews a new book that looks at the latest stage of the Palestinian struggle.</P></B>

<P>MOST STORIES about Israel's war refer to the start of the Palestinian uprising, or Intifada, 18 months ago. But this is only the latest stage in a rich history of struggle for justice.</P>
<P>"Ever since the beginning of the Middle Eastern conflict [in 1948], Palestinians have fought to achieve their legitimate national and civil rights," writes Ghassan Andoni in his contribution to <I>The New Intifada,</I> an excellent new collection of essays. "With the colonial and expansionist character of the Israeli occupation, resistance has continued on many different fronts, using a variety of methods."</P>
<P>The first Intifada began in 1987 as a massive and spontaneous upheaval against Israeli rule in the Occupied Territories of the West Bank and Gaza. "In 1987, Israel controlled every aspect of Palestinian life; there was no authority but the occupation authority," writes Andoni. "As a result, Intifada activities were neither confined nor localized. Protests and clashes occurred in every neighborhood and street. The 1987 Intifada was a genuine mass movement; most of the Palestinian population was directly involved in the resistance in one way or another. Methods ranged from peaceful protest and civil disobedience to limited violence."</P>
<P>The second Intifada that began 18 months ago has at times had a mass character, but there are differences. The most significant is that this uprising follows nearly a decade of "peace" negotiations begun with the 1993 Oslo accords--and the establishment of the Palestinian Authority (PA), led by Yasser Arafat.</P>
<P>The U.S. media portray the Intifada as the brainchild of Arafat--who prefers "violence" to "peace" and whose influence is so vast that he has the power to order terrorist attacks or to halt them with the snap of his fingers.</P>
<P>In reality, this uprising is an expression of the anger and bitterness that Palestinians feel at how little has been gained after Arafat compromised away many of their historical claims to return to homes that their ancestors were driven from in 1948.</P>
<P>Life for Palestinians worsened under Oslo in innumerable ways. What's more, Israel has been the source of the overwhelming majority of violence.</P>
<P>"Within the first six days of the Intifada, Israel's army and police had killed 61 Palestinians and injured 2,657, many of them children under the age of 18, and many of them killed or wounded from shots fired to the upper part of the body," writes Muna Hamzeh, a Palestinian journalist, whose chapter in the book tells the story of refugees living in the Dheisheh camp. "By comparison, during the same period, four Israelis--three soldiers and one settler--were killed, while 35 Israelis were wounded, most of them lightly."</P>
<P>"Nobody can convince me that we didn't needlessly kill dozens of children," a senior Israeli army officer told Israel's <I>Ha'aretz</I> newspaper early on in the conflict.</P>
<P>At the beginning of the Oslo process, Arafat agreed to Israel's sovereign right to control 78 percent of historic Palestine--a huge concession. But as far as Israeli negotiators were concerned, this was only the beginning--just "one component of the compromise," as Mouin Rabbani documents in his excellent chapter "A smorgasbord of failure: Oslo and the Al-Aqsa Intifada."</P>
<P>Describing the pattern of negotiations, Rabbani writes that "Israel first refuses to implement its own commitments, seeks and obtains their dilution in a new agreement, subsequently engages in systematic prevarication, and finally demands additional negotiations, leading to a yet further diluted agreement."</P>
<P>These simple facts and arguments are missing from almost all U.S. media accounts of the conflict--which is why <I>The New Intifada</I> is well worth reading. The conclusion is plain: Without justice for the Palestinians, there can never be peace.</P>

