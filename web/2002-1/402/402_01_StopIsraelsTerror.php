<P><font face="Times New Roman, Times, serif" size="4">Stop U.S. aid to Israel</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Stand up against Israel's terror</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 12, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>CORPSES PILED up in the morgues, or left rotting in the streets. Ambulances barred at checkpoints, while the wounded bleed to death. Palestinians kept prisoners in their own homes by Israel's siege.</P>
<P>This was the reality in the Israeli-occupied West Bank as <I>Socialist Worker</I> went to press amid the most savage military assault on Palestinians in two decades.</P>
<P>No one knew how many Palestinians had died since Israel's offensive began March 29. That's because few dared to leave their houses--for fear of being shot by Israeli snipers.</P>
<P>Desperation stalks Palestinian towns and refugee camps. Everywhere, there is a dire lack of food and medical treatment--made worse after Israeli forces cut electricity and destroyed water mains.</P>
<P>Israel's terror is designed to humiliate Palestinians--up to and including Palestinian Authority chief Yasser Arafat, still trapped on one floor of his headquarters in Ramallah more than a week after the invasion began. "I don't know if we can function," said Saeb Erekat, an adviser to Arafat. "This has set us back 10 years. The army has dismantled everything we tried to build."</P>
<P>While Israeli troops pull the triggers, this horror was made in the USA. The U.S. government hands billions of dollars worth of military hardware and high-tech weaponry to Israel every year.</P>
<P>And George W. Bush gave Israeli Prime Minister Ariel Sharon the green light for his latest offensive by repeating again and again that Arafat had to do more to combat "terrorism"--even as Sharon mounted ever more barbaric attacks on Palestinians.</P>
<P>Yet now the U.S. is trying to pose as a "peacemaker." Last week, Bush called on Israel to withdraw its invasion forces--and said that he was dispatching Secretary of State Colin Powell on an "urgent peace mission." It couldn't have been <I>that</I> urgent--since it took Powell almost a week to get to the Middle East, allowing Sharon to continue down his hit list of West Bank towns.</P>
<P>Whatever the outcome of Powell's mission, the U.S. bears responsibility for Israel's savage military repression. Washington has furnished Israel with the weapons it uses against Palestinians and provided political cover for its occupation--and it will continue to do so, even if Powell brokers a cease-fire and withdrawal.</P>
<P>That's why we need to continue organizing against Israel's terror--as hundreds of thousands of people did around the world last week. In the Middle East, protesters took to the streets in defiance of authoritarian Arab regimes, facing down riot cops armed with tear gas and water cannons.</P>
<P>Even in the U.S.--where the mainstream media's pro-Israel lies have seldom been challenged--protests in dozens of cities and towns attracted thousands. "I came to demonstrate against what's going on back in my hometown," Toufic Ibrahim, a Palestinian who lives in Chicago, told <I>Socialist Worker</I>. "We're doing our best to stop the killing of innocent people there. We also want to show people back home that we are supporting them--that they aren't in this struggle by themselves."</P>
<P>The U.S. props up Israel and its barbaric occupation with billions of dollars in aid every year. It's time to demand that the U.S. stop supporting Israel's terror--and that we step up the pressure to win justice for Palestinians.</P>

