<P><font face="Times New Roman, Times, serif" size="2"><B>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="3"><b>Taking sides in a battle between oppressor and oppressed</b></font><BR>
<A HREF="402_03_SideWithPalestine.shtml"><font face="Times New Roman, Times, serif" size="4"><b>We stand with the Palestinians</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">This week marks the Warsaw Ghetto Uprising of 1943, in which the lightly armed Jewish Fighting Organization heroically resisted Nazi armed forces. Today, lightly armed Palestinians are heroically resisting the onslaught of the Israel Defense Forces. But the Palestinians are reviled as "terrorists."</P>

<P><font face="Times New Roman, Times, serif" size="2"><B>WHAT WE THINK</b></font><BR>
<A HREF="402_03_PeaceMakerForWar.shtml"><font face="Times New Roman, Times, serif" size="4"><b>"Peacemaker" who's out for blood</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">Many names come to mind when you think of George W. Bush. "Peacemaker" should <I>not</I> be one of them. But that's how Bush tried to appear as he sent Secretary of State Colin Powell to the Middle East last week.</P>

<P><A HREF="402_08_SharonIsTerrorist.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Ariel Sharon: This man is the real terrorist</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">The state of Israel came into existence a half-century ago--and has maintained itself ever since--through terror, warfare and the illegal seizure of Palestinian lands. And Prime Minister Ariel Sharon has had blood on his hands from the very beginning.</P>

<P><A HREF="402_09_NewIntifadaReview.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Roots of the new Palestinian Intifada</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">Most stories about Israel's war refer to the start of the Palestinian uprising, or Intifada, 18 months ago. But this is only the latest stage in a rich history of struggle for justice.</P>

<P><font face="Times New Roman, Times, serif" size="2"><B>READING BETWEEN THE LINES</b></font><BR>
<A HREF="402_09_MediaLiesOnIsrael.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Media miseducation about Israel's barbarism</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">A 1991 survey showed that the more people watched TV news about the Gulf War against Iraq, the more pro-war they became--and the less they actually learned about the war. Multiply that effect by 10, and you have the mainstream media's coverage of Israel's war against the Palestinian people.</P>


