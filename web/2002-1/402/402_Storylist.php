<p><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>ISRAEL'S WAR ON PALESTINIANS</b></font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>Trapped in her own house by Israel's siege:</b></font><BR>
<A HREF="402_02_WeWontGiveUp.shtml"><font face="Times New Roman, Times, serif" size="4"><b>"We won't give up"</b></font></A><BR>
