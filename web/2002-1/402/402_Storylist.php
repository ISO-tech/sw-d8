<P><font face="Times New Roman, Times, serif" size="3"><b>Free all the detainees now!</b></font><BR>
<A HREF="402_12_FreeDetainees.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Being Arab is not a crime</b></font></A><BR>
