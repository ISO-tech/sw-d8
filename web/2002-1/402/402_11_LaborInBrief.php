<p><font face="Times New Roman, Times, serif" size="4"><b>San Diego <I>Union Tribune</b></font></P></I>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Chuck Stemke</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SAN DIEGO--About 40 pressroom workers and their supporters picketed the headquarters of the <I>Union Tribune</I> (UT) newspaper in the posh La Jolla shopping area on March 25. "We are naming [<I>Tribune</I> owner and billionaire] David Copley San Diego's corporate pig of the year," shouted Jeff Alger, a fired pressroom worker and the secretary-treasurer of Graphic Communications International Union (GCIU) 432M, amid chants of "UT--Unfair!"</P>
<P>Union members have picketed management functions, broadcast commercials, flown planes with tow banners over Padres games, and basically harassed their bosses for years now. The fact that they have not received a raise in nine years sits at the top of a long list of grievances.</P>
<P>Juan Avalos, an apprentice, spent much of the rally passing out flyers to passing cars, saying, "All I want is to raise my daughter right&#133;excuse me, take a flyer, support the working class!"</P>
<P>"Big corporations are the ones who act like they represent the whole voice of the people," he told <I>Socialist Worker</I>. "But they are also the ones who mistreat their workers, who are the basis of this whole country," referring to the<I> UT</I>'s near-monopoly in San Diego.</P>
<P>Alger is confident that the public hasn't gone completely over to George W. Bush and the fat cats. "The pendulum is swinging in the direction of working people," he said.</P>

