<p><a name="immigrant"></a><font face="Times New Roman, Times, serif" size="4"><b>Los Angeles immigrant workers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Karl Swinehart</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LOS ANGELES--Chanting "Don't eat where the owners cheat!" and "Do Won Chang, you're no good, pay your workers like you should!" 100 people demonstrated outside Take Sushi and Assi Market in Koreatown on March 22. The event brought together garment workers, grocery store workers, clergy and community supporters.</P>
<P>The owner of Take Sushi--Do Won Chang--owns the clothing line Forever 21, and he owes 19 workers hundreds of thousands of dollars in back pay and overtime. The workers sewed Forever 21 clothes in factories in downtown LA without the guarantee of overtime or minimum wage, working 10 to 12 hours a day in dirty, unsafe factories. </P>
<P>"There came a time when I said to my boss that I wasn't earning enough and wanted the minimum wage--he fired me," Esperanza, a worker at the factory, told <I>Socialist Worker</I>. "He said that the fact that I didn't have papers meant that I didn't have rights."</P>
<P>The picket ended with a march through the parking lot of Assi Supermarket where a recent union election ended in a 76-to-76 tie--with two ballots missing. Leading up to the vote, managers went on a campaign of harassment against union activists, holding closed-door meetings to intimidate workers--and supervisors voted in the election.</P>
<P>Korean Immigrant Workers Advocates estimates that Assi grosses at least $750,000 a week--almost twice that of a typical grocery store--while workers receive the minimum wage, have little job security and no benefits. </P>
<P>But Assi workers are determined to change this situation and are finding allies with other workers--immigrant and native born.</P>

