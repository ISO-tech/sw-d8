<p><a name="Local 6"></a><font face="Times New Roman, Times, serif" size="4"><b>ILWU Local 6</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Ken Morgan, ILWU Local 6</b></font> </P>
<font face="Times New Roman, Times, serif" size="3"><P>SAN FRANCISCO--Members of International Longshore and Warehouse Union (ILWU) Local 6 are fighting to take back their union.</P>
<P>After electing new officers in a landslide victory in November 2001, Local 6 members assumed that they had reasserted control over their local. They gave the new officers near unanimous votes of support at all five division membership meetings, and again at the local convention in February.</P>
<P>It appeared that the defeated forces around former local President Roberto Flotte and his brother, former local organizer Alfredo Flotte, would accept the decision of the membership. Apparently that is not the case.</P>
<P>Roberto Flotte had a long meeting with his ally, ILWU International President James Spinosa, in early March. Shortly thereafter, Spinosa sent a letter to the local's leadership, virtually accusing the local of mounting an insurrection, because it seated all the newly elected officers.</P>
<P>Spinosa threatened to put the local in receivership but has said he will hold off until the U.S. Department of Labor releases its findings on complaints and charges from several parties. The issues involve past elections and possible financial wrongdoing by the previous administration.</P>
<P>These latest developments are regarded by most of the membership of Local 6 as an attempt by the former discredited administration to overturn the mandate of the membership, through an alliance with the International leadership. Several other ILWU locals have publicly come out in support of the membership of Local 6.</P>
<P>The International needs to recognize the decision of the Local 6 membership and to recognize the results of the past election and convention. </P>

