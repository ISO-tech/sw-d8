<P><font face="Times New Roman, Times, serif" size="5"><b>Saying no to their cutbacks</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 12, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Students, faculty and campus workers recently were shaken to hear that tuition is being raised by $220 and that four faculty members are scheduled to be "retrenched" at Fulton-Montgomery Community College in New York. </P>
<P>This week, 15 students and I hit the hallways to announce a meeting against the tuition increases and layoffs. Approximately 150 students came for the meeting--only to find that we were locked out of the theater. The crowd, determined to have their meeting, went to the cafeteria, chanting "Whose school? Our school!"</P>
<P>Students decided to speak with the college president. About one-third of the students walked into the administration building--at which point the vice president ran toward the door and blocked it like a linebacker. One student was arrested after being pushed by the vice president. </P>
<P>The only way to prevent the tuition hike and layoffs is for the entire college community to unite and take the administration head-on.</P>
<B><P>Steven Gottlieb,</B> Johnstown, N.Y.</P>

