<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Taking sides in a battle between oppressor and oppressed</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>We stand with the Palestinians</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 12, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THIS WEEK marks the annual commemoration of the Warsaw Ghetto Uprising of 1943, in which the lightly armed Jewish Fighting Organization in Poland heroically resisted Nazi armed forces equipped with artillery, tanks and fighter planes.</P>
<P>One of the few young socialist fighters who survived, Chaike Belchatowska Spiegel, died last week at age 81. "She encouraged Jews to resist being moved [to death camps] by every means possible," the <I>New York Times</I> recalled in her obituary.</P>
<P>Today, lightly armed Palestinians are resisting being moved from their land by every means possible--and the Israel Defense Forces (IDF) are attacking densely populated areas, using artillery, tanks and fighter planes.</P>
<P>But the <I>New York Times</I> will never refer to the Palestinians as freedom fighters and heroes. Instead, they're reviled as "terrorists."</P>
<P>Yet at least one person in Israel was honest about the horrific parallel with the Warsaw Ghetto Uprising. "If our job is to seize a densely packed refugee camp or take over the Nablus casbah, and if this job is given to an officer to carry out without casualties on both sides, he must before all else analyze and bring together the lessons of past battles, even--shocking though this might appear--to analyze how the German army operated in the Warsaw ghetto," an IDF officer told the Israeli newspaper <I>Ma'ariv</I> last month.</P>
<P>As British journalist Robert Fisk wrote, "Does this account for the numbers marked by the Israelis on the hands and foreheads of Palestinian prisoners earlier this month? Does this mean that an Israeli soldier is now to regard the Palestinians as sub-humans--which is exactly how the Nazis regarded the trapped and desperate Jews of the Warsaw ghetto in 1944?"</P>
<P>Israeli Prime Minister Ariel Sharon and George W. Bush argue that Israel has the "right to defend itself." Unlike Palestinian suicide bombers, they say, the IDF doesn't "intend" to kill civilians.</P>
<P>But the reality is that the Israeli state came into being in 1948 using terror and mass murder. And Israel has maintained itself ever since through war, conquest and occupation. As pro-Palestinian Israeli activist Ran Cohen wrote of the current attacks, "The question of intention is a poor refuge. If you enter Ramallah with 150 tanks, cutting water supply and medical aid, if you drop tons of bombs on Afghanistan or Iraq, don't tell me you never intended to harm civilians."</P>
<P>Remember, moreover, that the U.S. routinely denounced Nelson Mandela's African National Congress as "terrorists" in the 1980s--while hailing Islamists in Afghanistan as "freedom fighters" against the old USSR. Washington has no right to make sanctimonious statements about terror--especially when it is planning an all-out terror campaign of its own against Iraq.</P>
<P>Suicide bombings of civilian targets are counterproductive. They kill indiscriminately, waste the lives of liberation fighters and give Sharon the pretext for more savage repression. But Palestinians face oppression so great that growing numbers of young people would rather die resisting Israel's iron heel--just as members of the Jewish Fighting Organization concluded that it was better to die fighting the Nazis than to be sent to concentration camps.</P>
<P>Many people appalled by Israel's onslaught believe that the violence of both sides must be condemned. For example, Medea Benjamin of Global Exchange last week called for a UN peacekeeping force to "help guarantee the security of both Israelis and Palestinians."</P>
<P>But one side in this conflict is fighting to maintain a colonial-settler state based on racial and religious exclusion and backed by overwhelming military force and the imperialist might of the world's only superpower. The other side stands for the national liberation of an oppressed people driven from their homeland and denied the right of return.</P>
<P>That is why we must unconditionally support the struggle for self-determination for Palestinians--and fight for a democratic and secular Palestine in which Jews, Arabs, Muslims and Christians have equal rights.</P>

