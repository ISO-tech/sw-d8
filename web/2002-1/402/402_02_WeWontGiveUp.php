<P><font face="Times New Roman, Times, serif" size="4">Trapped in her own house by Israel's siege:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"We won't give up"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 12, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>SUZANNE ABU TAIR lives in Bethlehem. She's a nurse in the Dheisheh refugee camp outside the town--where some 12,000 people live on half a square mile of land. Suzanne spoke to<I> Socialist Worker</I>'s PAIGE SARLIN last Friday about the desperate conditions that Palestinians face today.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<P>THINGS ARE very bad here in Bethlehem. We'll hold on and on, even if we are dying. But we are not living.</P>
<P>I live in the old city near Manger Square and the Nativity Church. It's under total military closure. I just saw on local television--I'm one of the lucky families that has electricity--that the Israelis are lifting the curfew for three hours for people to go out and get food and water. </P>
<P>But the curfew was lifted only for certain areas. The old city is still under total curfew--so people are reporting that if any cars try to move, the Israelis shoot. So that means that people don't know if they can go outside or not--it makes for an extremely dangerous situation.</P>
<P>My two young sons keep asking when they can leave, when can they go out--and I keep telling them, "Soon." Last night, I went out on an adventure, defying the curfew.</P>
<P>You can't believe how they are treating us--treating the old city. Some who are resisting have been shot, and they bleed until they die. In the meantime, the soldiers are breaking everything--the infrastructure, the water supply, breaking down the doors of all the shops and leaving nothing inside. The streets have been demolished, and the cobblestones of the old city are completely torn up. The windows are all broken, and demolished cars are everywhere.</P>
<P>If you look around, it's like we were living in a war for a year already--and it's barely been a week. We aren't living. We can't move, we can't do anything.</P>
<P>The Oslo process was totally false. You can't go to Jerusalem or anywhere even if you have a permit. We want an independent state, return for the refugees, a free Palestine. Even the children ask, "When can we move [out of the camp]?" I tell them we need to wait.</P>
<P>There are orders to demolish the house of Ayat al-Akhras' family--the 18-year-old girl who blew herself up outside a supermarket in Jerusalem. The Apache helicopters haven't come to fire missiles into their home, which lies in the center of an extremely dense area of the camp--but everyone in the camp knows that it will happen soon. </P>
<P>There are soldiers in all of the alleyways and tanks at every entrance and exit to the camp. The soldiers have taken over many buildings, and the curfew is total. </P>
<P>The soldiers won't hesitate to attack the ambulances. Last night, they demolished a Red Crescent ambulance, but by today, it has vanished--so there's no evidence to prove how brutal they're being.</P>
<P>Have you seen the horrible photos of the 60-year-old woman and her son who bled to death? They lie in the Beit Jala morgue with four others, one of whom is Tamir Ibrahim Solmar, who was trying to ring the bells of the Nativity Church when a sniper shot him dead. You can't say these people were terrorists.</P>
<P>The terrorist attacks happen because there are no alternatives--how can we fight these Apaches and F-16s? We are living in a war and in prison. But we will not give up. We will live in dignity or die bravely.</P>

