<P><font face="Arial, Helvetica, sans-serif" size="2"><b>INSIDE THE SYSTEM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The curse of the union</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 12, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WHY WOULD workers at the Mt. Sinai-St. Francis Nursing and Rehabilitation Center in Miami, Fla., vote in favor of union representation? Could it be the long hours and low wages? Lack of good benefits?</P>
<P>Not at all, according to the nursing home's management. It's because the union used voodoo to scare Haitian workers into voting in favor of unionizing.</P>
<P>In late February, workers voted 49-37 to unionize, citing low wages, unaffordable health insurance and poor treatment from management. But nursing home lawyers recently told the National Labor Relations Board that the election should be overturned because the Service Employees International Union (SEIU) placed "voodoo symbols," like lines of pennies and half-empty waters cups, around the nursing home to "intimidate employees" into joining a union. </P>
<P>Since the election, the nursing home has intensified anti-union harassment. According to an SEIU spokesperson, workers' benefits have been frozen and management has circulated an anti-union petition.</P>
<P>One pro-union nursing assistant at the facility was fired after she recently refused to collect soup bowls, one by one, from residents and bring them to the kitchen.</P>
<P>--Associated Press, March 21, 2002</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>No more free lunch for senators</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>PITY THE state senators from Washington. They're about to lose their private lunchroom and special French chefs.</P>
<P>Next year, the Washington legislature is moving temporarily while the Capitol undergoes a $100 million renovation. As a result, the senators will be forced to find other dining arrangements--which is leaving some of them irate.</P>
<P>"THIS IS UNACCEPTABLE," fumed state Sen. Don Benton in a letter signed by 36 of his fellow 49 state senators. "We, as members of the Senate, have come to look forward to the quality food prepared by Jean-Pierre and Kerri, as well as the quiet camradery of our fellow senators in a private setting."</P>
<P>The senators recently honored their private chefs, Jean-Pierre and Kerri Simon, with a standing ovation--at the same time as they wrote a budget that cut $685 million in state services and laid off hundreds of state employees.</P>
<P>--<I>The Olympian,</I> March 21, 2002</P>

