<P><font face="Times New Roman, Times, serif" size="5"><b>Lockheed Martin</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Tony Kin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 12, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MARIETTA, Ga.--To listen to the mainstream media here, you would think the month-long Lockheed Martin strike was about to collapse.</P>
<P>Workers say differently. "Personally I'm prepared to stay out as long as it takes," Marvis Kight, who has worked at Lockheed Martin for 39 years, told <I>Socialist Worker</I>.</P>
<P>The plant, which makes fighter jets and transport planes for the U.S. military, has slashed union jobs through outsourcing. "It's not about the money. We're out here for decent retirement and health benefits. You've got people 70 years old still in [Lockheed] because they can't pay the insurance. Otherwise, you have to get another job or use your savings to pay the insurance. We're tired of being treated like stepchildren."</P>
<P>On April 2, the Federal Mediation and Conciliation Service intervened and requested to meet with union and company negotiating committees. The media is using this to portray striking International Association of Machinists Local 709 members as defeated and ready to give in.</P>
<P>On the picket line, however, the story is quite different. David Axley, an employee of 22 years, isn't worried about the meeting with federal mediators. "Just because they're meeting doesn't mean it's over with, not while we're here," said Axley.</P>
<P>Axley, like many Lockheed workers, hopes the strike will end soon, but doesn't want to settle for a contract that fails to protect jobs and benefits. As another striker put it, "First-rate people should not have to work for second or third rate contracts."</P>
<P>Solidarity remains strong. Strikers are continuing to picket the Lockheed plant 24 hours a day, seven days a week.</P>
<P>Management's response has been to train salaried supervisors to do the machinists' jobs. Workers believe their collective experience and skill level are irreplaceable and plan to use that advantage to get a better contract.</P>
<P>We need to continue demonstrating our solidarity--and to point out the absurdity of a system where a company that receives a $200 billion government contract for military production can't guarantee job security or provide decent benefits for its workers.</P>

