<P><font face="Times New Roman, Times, serif" size="5"><b>Taking to the streets to protest Israel</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 12, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>PEOPLE ACROSS the country took to the streets last week to protest Israel's war on Palestinians. From Los Angeles to Washington, D.C., and dozens of cities in between, demonstrations and marches drew hundreds, and even thousands of people.</P>
<P>Some 5,000 people marched through downtown Chicago April 6. "They say that justice is blind, but justice isn't blind when it comes to American tax dollars going to the Israeli government and military," Anam, a Palestinian who lives in Chicago, told <I>Socialist Worker</I>.</P>
<P>That same day, more than 3,000 New Yorkers marched across the Brooklyn Bridge--menaced by police numbering in the hundreds. Marchers chanted slogans like "Bush buys the bullets, Sharon pulls the trigger." In LA, more than 1,500 marched on April 6. In Washington, D.C., about 700 demonstrated in front of the Israeli embassy April 4.</P>
<P>Smaller demonstrations took place across the U.S. In Austin, Texas, more than 200 gathered on the steps of the Capitol, flying Palestinian flags and chanting "Free Palestine." In Vermont, there were <I>nine</I> demonstrations on April 6 against Bush's war and in support of Palestinians. </P>
<P>The outrage in the U.S. was matched by even bigger protests around the world. In Paris, as many as 40,000 came out on April 6 to show their support for Palestinian liberation. Some 20,000 protesters marched through the streets of Rome to the Piazza del Popolo, where the crowd swelled to about 50,000 people. In Indonesia, more than 1,000 protesters clashed with riot cops.</P>
<P>The protests in the Arab world are especially significant--because of the great sympathy for the Palestinian cause and because of the potentially explosive tensions that characterize nearly every country in the region.</P>
<P>Bahrain and Jordan saw violent clashes between demonstrators and police, but the largest march--estimated at 1 million--took place in Morocco's capital of Rabat. For seven days in a row, crowds of 15,000 turned out to protest in Baalbek, Lebanon. In Egypt, tens of thousands protested in defiance of a government ban on demonstrations.</P>
<P>These inspiring protests can be the springboard to an international movement in solidarity with the Palestinians.</P>

