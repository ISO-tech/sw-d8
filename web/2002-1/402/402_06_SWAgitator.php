<P><font face="Times New Roman, Times, serif" size="5"><b>Agitator and organizer</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 12, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>ALAN MAASS, the editor of <I>Socialist Worker,</I> argues for the importance of a revolutionary newspaper.</P></B>
<P>WHEN A fellow New York City journalist gave a toast praising "freedom of the press," John Swinton minced no words.</P>
<P>"There is no such thing, at this date of the world's history, in America, as an independent press," Swinton told the other reporters gathered for a banquet in his honor. "You know it, and I know it. There is not one of you who dares to write your honest opinions, and if you did, you know beforehand that it would never appear in print&#133;We are the tools and vassals of rich men behind the scenes. We are the jumping jacks. They pull the strings, and we dance."</P>
<P>Swinton--at one time a managing editor of the <I>New York Times</I> who later launched his own left-wing weekly called <I>John Swinton's Paper</I>--said this more than a century ago. But for anyone who followed the mainstream media during George W. Bush's war drive--from the carpet bombing of Afghanistan to the U.S. government's support for Israel's war on Palestinians--his words ring true today.</P>
<P>This is the first reason why a paper like <I>Socialist Worker</I> is so important. If people in the U.S. knew the truth about what's carried out in their name around the globe, it would turn their stomachs. But they won't learn more than isolated facts from the corporate-run media.</P>
<I><P>SW</I> tells the truth about the crimes of the U.S. government--and all the powers that be, from greedy corporations to corrupt politicians to the brutal police and fanatical prosecutors of the U.S. injustice system. And we report on the resistance to those on top--from small everyday struggles to the mass revolts that shake whole societies.</P>
<P>For this, we're often criticized for not being "objective." We aren't. We proudly take the side of our class--working people and the oppressed--against those who hold power over their lives.</P>
<P>But producing a socialist newspaper is about more than telling the truth. We want to convince readers not only about what's wrong with the world--but that they need to act to change it.</P>
<P>As the Russian revolutionary Lenin put it, "A newspaper is not only a collective propagandist and a collective agitator, it is also a collective organizer. In this last respect, it may be likened to the scaffolding around a building under construction, which marks the contours of the structure and facilitates communication between the builders, enabling them to distribute the work and to view the common results achieved by their organized labor."</P>
<P>Our goal is for <I>SW</I> to be the thread that ties people together in struggle--not only providing the information they need to know, but becoming a forum for discussing the issues of the movement and a tool for involving others.</P>
<I><P>Socialist Worker</I> turned 25 years old this month, and thanks to the effort and hard work of many people, we're closer to that goal. We've become a bigger and better paper--a weekly publication sold to thousands of people across the country.</P>
<P>Every reader has a part to play in helping <I>SW</I> to grow. If you're seeing <I>SW</I> for the first time, we hope you'll become a regular reader. You can subscribe by filling out the form on the back cover--or you can get a copy at an <I>SW</I> sale in your city or town each week. If you're already a regular reader, we hope you'll get involved with the International Socialist Organization.</P>
<P>Or consider getting a bundle of 5 or 10 papers each week to sell to your coworkers, neighbors and friends as a focus for discussions and organizing. You can find out more about receiving bundles by calling 773-583-6725, or e-mailing circulation@socialistworker.org.</P>
<P>After 25 years of speaking out for our side, we look forward to the future.</P>

