<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>ULLICO spells union corruption</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 12, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WHEN THE AFL-CIO posted a Web page on "Curbing Corporate Greed" three years ago, Union Labor Life Insurance Co. (ULLICO) got a nod as a pro-worker alternative. "ULLICO and all union-fund investors focused on the future are guided by the same principle: Working families' pension fund assets must be invested for the long term in ways that respect those to whom it ultimately belongs."</P>
<P>But it turns out that the top union officials on the ULLICO board of directors were indulging in some corporate greed themselves--and are now under investigation by federal officials. According to recent reports in the <I>Wall Street Journal</I> and <I>Business Week</I>, union officials used ULLICO funds to stuff millions of dollars into their pockets with Enron-style insider deals involving Global Crossing, the now-bankrupt telecommunications company.</P>
<P>At the top of the list was Robert Georgine, who recently retired as head of the AFL-CIO Construction Trades Department after 26 years and remains chair of ULLICO. Georgine--who was notorious for using a private jet at union expense--has long been one of the leading conservatives on the AFL-CIO Executive Council. "From his perch as head of ULLICO, he does the same thing as he did as head of the building trades, which is selling out the members he claims to represent," said Mike Orrfelt, editor of <I>Hard Hat,</I> an independent magazine for construction union members.</P>
<P>In 1997, Georgine took advantage of connections with top Democratic Party operatives to invest ULLICO funds in Global Crossing. Georgine and other union officials were able to profit personally as well--at the expense of the unions they represent. </P>
<P>Until 1997--the year of the Global Crossing investment--ULLICO shares were limited to union officials and priced at $25. Since then, the price of the stock has been set by the board of directors once a year to reflect changes in the value of the investment. As a result, union officials on the board of directors knew that if Global Crossing was going up, ULLICO stock would go up, too--and could make investment decisions accordingly.</P>
<P>In December 1999, Georgine secretly gave ULLICO directors and officers a chance to buy stock before the price was increased to reflect gains in Global Crossing. But when Global Crossing stocks tanked, most union board members sold ULLICO stock at $146 per share--because they knew that ULLICO stocks would soon be reduced to $75 per share.</P>
<P>Total profit for directors: $6.5 million.</P>
<P>Martin Maddaloni, president of the Plumbers' union, admits that he pocketed $184,000 in 2000. Other top officials who sold off large numbers of shares include Communications Workers of America President Morton Bahr; Carpenters' union President Doug McCarron; William Bernard, former head of the Asbestos Workers union; and Jacob West, past president of the Ironworkers.</P>
<P>AFL-CIO President John Sweeney, who also sits on the board, says that he didn't sell ULLICO shares. Yet even if Sweeney didn't profit personally, he didn't speak out about the scheme. That's because his instinct is to protect the bureaucracy that nurtured his decades-long career as a union official.</P>
<P>"Trade union life insurance, like other forms of trade union capitalism, works injuriously upon the labor organizations," William Z. Foster, a veteran labor and Communist Party leader, wrote of ULLICO shortly after it was founded in 1925. "It diverts their attention from the struggle and into capitalist enterprises. It poisons the organizations with an anti-working class ideology, and subordinates them organizationally to capitalist institutions. It corrupts the leaders, enriches them and makes them less and less responsive to rank-and-file interests and control. It is a menace to the labor movement."</P>
<P>The menace is greater than Foster could have imagined. Holding union leaders accountable over dealings such as ULLICO must be part of the fight for a democratic, fighting labor movement.</P>

