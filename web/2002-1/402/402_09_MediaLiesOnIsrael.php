<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Media miseducation about Israel's barbarism</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 12, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>A 1991 University of Massachusetts survey showed that the more people watched television news about the Gulf War against Iraq, the more pro-war they became--and the less they actually learned about the war.</P>
<P>Multiply that effect by 10 times, and you have the mainstream media's coverage of Israel's war against the Palestinian people.</P>
<P>The U.S. press, like the government from which it takes its cues, has always been pro-Israel. But in the ongoing war in the Middle East, the media have deliberately miseducated the public about what's really happening in Palestine.</P>
<P>Like the Bush administration, the media have framed the conflict as Israeli self-defense against what the <I>New York Times</I> called "the cancer of suicide bombing." The reality--that Israel is perpetuating and extending a military occupation over more than 3 million people--is flushed down the memory hole.</P>
<P>Alan Keyes, the former far-right Republican presidential candidate who now hosts his own MSNBC show, even insists that Israel isn't an occupying power! If Keyes is right, then how could Israel prevent Yasser Arafat from attending the Arab League summit in March? </P>
<P>In the current climate, the most ignorant yahoos on cable news shows feel perfectly comfortable badgering any guest who makes an effort to tell the Palestinian side. During the April 3 episode of CNN's <I>Crossfire</I> filmed at George Washington University, conservative Tucker Carlson and liberal Paul Begala incited a near lynch-mob atmosphere against an Arab student who tried to question Israel's war.</P>
<P>Meanwhile, CNN's Christiane Amanpour, in an interview conducted in Arafat's Israel-occupied headquarters, demanded that Arafat crack down on suicide bombers. Conducted in a room without water or electricity--showing Arafat at his most powerless--the interview said more about Amanpour's arrogance than about Arafat's alleged responsibility for "terrorism."</P>
<P>Meanwhile, the media leave the most extreme lies from the Israelis unchallenged. Israeli ambassador to the U.S. Mark Regev regularly denies that Israel has targeted civilians during its 18-month repression of the Intifada.</P>
<P>The same journalists who try to extract condemnations of suicide bombing from Palestinian spokespeople--in English and Arabic, no less--don't seem to care that Regev is lying to them. But every major human rights organization, from Amnesty International to the Israel-based B'Tselem, has documented a deliberate Israeli policy of targeting civilians.</P>
<P>Of the 893 Palestinian civilians killed by Israeli gunfire during the Intifada until March 30, 192 have been children. The entire Israeli operation in the West Bank has targeted civilians--from cutting off water to whole towns, to preventing ambulances from helping the wounded, to wanton murder of people in their homes.</P>
<P>If the U.S. media are willing to echo the Sharon government's view of events, it's because they have accepted the indefensible in their coverage of America's "wartime" leaders.</P>
<P>Why make a fuss over Israel's mass arrests and imprisonment of detainees in concentration camps when the media have made barely a peep over the U.S. government's own prison camp at Guant&aacute;namo Bay, Cuba? Why grill the Israeli military over civilian casualties when the press accepts Defense Secretary Donald Rumsfeld's denials of Pentagon-engineered civilian deaths in Afghanistan?</P>
<P>In early April, the elite media began to demand that the Bush administration intervene to solve the Middle East crisis. Bush announced that he would send Secretary of State Colin Powell to the region to broker a deal.</P>
<P>But will the media actually look at the administration's role? Will they look to see if Powell insists that Israel end the occupation--or merely repackage it? Will they say so if the U.S. simply seeks a "cease-fire" that will allow Israel to finish its dirty work? In short, will they actually tell the truth about what's happening in the Middle East?</P>
<P>If their sorry performance so far is any indication, don't count on it.</P>

