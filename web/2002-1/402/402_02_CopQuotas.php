<P><font face="Times New Roman, Times, serif" size="5"><b>Arrest quota is a recipe for profiling</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 12, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE HEAD of the Chicago Police Department unit that patrols the Cabrini-Green public housing project on the city's Near North Side decided earlier this year that his officers weren't being "productive" enough. So Lt. Michael Fitzgerald demanded more arrests from his cops--and implemented monthly quotas to make sure he got them.</P>
<P>According to a memo that Fitzgerald sent to his officers--which was leaked to the press in late March--unless public housing cops arrest four people every 28 days, they will receive unfavorable performance ratings.</P>
<P>Fitzgerald did give the cops other options to meet their quotas--make at least one traffic stop or write at least two curfew or truancy tickets a day, for instance.</P>
<P>The quotas are a recipe for racial profiling and harassment by cops. "This is old-line policing that predictably generates illegal stops of pedestrians and motorists," Harvey Grossman, of the Chicago office of the American Civil Liberties Union, told the <I>Chicago Tribune.</P>
</I><P>But Chicago police spokesperson Pat Camden saw nothing wrong with the new quota system. "It's just "unusual to see it in writing," Camden told the <I>Tribune</I>. "This isn't unreasonable. There has to be a way to account for an officer's time."</P>

