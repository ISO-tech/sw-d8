<P><font face="Times New Roman, Times, serif" size="5"><b>City University of New York</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By David Thurston</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 12, 2002 | Page 10</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--About 100 students from the City University of New York (CUNY) were joined by faculty and other supporters at a rally at City Hall on April 5. The students testified at city council hearings in support of a resolution opposing Gov. George Pataki's proposed cuts to the Tuition Assistance Program (TAP).</P>
<P>Pataki wants to cut yearly TAP stipends by one-third--and instead give the money back to students after graduation, as "an incentive" to finish a degree. This is a cruel hoax. Many of the 71,000 TAP recipients in the CUNY system won't be able to stay in school without the extra money. </P>
<P>The TAP cutbacks are part of a barrage of attacks. Undocumented immigrants have already had their tuition doubled. CUNY is ending its policy of making students' last semester free. And millions of dollars in city-funded scholarships are also on the chopping block.</P>
<P>Barbara Bowen, president of the Professional Staff Congress, CUNY's faculty union, told the April 5 rally that the cuts "are a message that we don't want to educate you if you aren't rich and white." Bowen has pledged faculty support for the campaign against the cuts.</P>
<P>Leading liberal politicians also spoke at the rally--Charles Barran, a veteran of the Black Power movement; Harlem's Bill Perkins; and Carl McCall, the state comptroller and New York's top Black official. Some of these politicians may be sincere in their opposition to the TAP cuts. But students can't rely on them to stop Pataki.</P>
<P>Throughout the 1990s, lobbying against wave after wave of cuts to CUNY didn't stop the budget ax. Only protests of tens of thousands in 1996 forced Pataki to back off the worst of his proposals.</P>
<P>Activists are planning a citywide protest for April 30 at 2 p.m., with a march from Union Square to City Hall. But for the rally to be big, we need to get the word out about these cuts--and organize people to speak out.</P>

