<P><font face="Times New Roman, Times, serif" size="5"><b>Ozomatli's catchy call to be active</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Chuck Stemke</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 5, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>MUSIC: </B>Ozomatli, <B><I>Embrace the Chaos,</B></I> Interscope Records, 2001.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>IT SEEMS like every time you go to a meeting to organize a protest here in Southern California, someone says, "We should see if we can get Ozomatli to play!"</P>
<P>And for good reason. Not only does this band really get the crowd going, but their music ties in perfectly with the activist culture of the region. Ozomatli's most recent album, <I>Embrace the Chaos, </I>which won a Grammy, is a call to get active.</P>
<P>The record is obviously influenced by the group's performance in front of 12,000 protesters at the 2000 Democratic National Convention--where the LAPD pulled the plug on them, which you can hear on the title track.</P>
<P>"When we were there, we were given a lot of energy from the people who were there to protest--how they felt about our involvement in the protests," band member Ulises Bella says on the group's Web site. "It was really inspiring."</P>
<P>Ozomatli is a seven-piece, multiracial band, made up of Black, Chicano, Cuban, Japanese, Jewish and Filipino members--and with a style as versatile as it is unique. They go from moody to hyped-in tempo, from hip-hop to Cumbia to Afro-Latin in influence, as easily as they go from English to Spanish in language. Whatever the style, though, there's always plenty of percussion and catchy choruses.</P>
<P>Definitely check out "Dos Cosas Ciertas," and turn up the volume for the ridiculously hip "Vocal Artillery."</P>
<P>Now, does anyone know how to get them to play a peace concert on the border this June?</P>

