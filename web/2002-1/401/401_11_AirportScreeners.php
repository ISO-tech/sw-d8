<P><font face="Times New Roman, Times, serif" size="5"><b>Defend immigrant workers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Brian Cruz</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 5, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IMMIGRATION AND Naturalization Service (INS) agents raided San Francisco and San Jose International Airports last week, arresting 25 service workers for immigration violations. The raid was part of a nationwide sweep of airports that began in the weeks following September 11 as part of the Bush administration's attack on immigrants.</P>
<P>The food handlers, janitors, baggage loaders and other workers detained were found to either be unauthorized to work in the U.S. or to have felony criminal records. But most of the felony convictions stem from giving false social security numbers in order to get low-wage jobs.</P>
<P>The INS has made similar arrests in Seattle, Portland, Ore., Salt Lake City, Atlanta, Boston and Las Vegas.</P>
<P>This and other recent crackdowns by federal authorities reveal the government's stance toward immigrants as a whole--legal or not. For example, similar excuses were given to justify the mass firing of thousands of legal residents working as airport security screeners--who are to be phased out by November under the recently passed Aviation and Transportation Security Act, under which only U.S. citizens can have these jobs.</P>
<P>These workers aren't losing their jobs because of their abilities. They are being scapegoated.</P>
<P>But workers and their unions are fighting back. In San Francisco, the screeners--members of Service Employees International Union Local 790--have mounted an effort to defend their jobs, backed by a coalition of immigrant rights groups. Their fight was a key focus of a 200-strong demonstration March 23 organized by San Francisco Labor for Peace and Justice.</P>
<P>The AFL-CIO is on the record as supporting immigrant rights--including amnesty. Now is the time to make good on that pledge--and defend these workers' jobs.</P>

