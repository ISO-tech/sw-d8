<P><font face="Arial, Helvetica, sans-serif" size="2"><b>INSIDE THE SYSTEM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Birdbrained logic</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 5, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. Navy recently tried to prove its love for birds. Environmental groups and bird-watcher Ralph Frew filed a lawsuit claiming that the Navy's bombing and shelling exercises on the tiny Pacific island of Farallon de Medinilla was creating an environmental disaster and killing the native bird population.</P>
<P>The Navy said that it had to keep using the island because it was the only available practice range in the Pacific. And officials insisted that dropping bombs on the island is actually good--for both birds and bird-watchers!</P>
<P>According to Navy lawyers, using the island as a live-fire range keeps people from getting on the island and harming the wildlife. Of course, they had no comment about the birds that end up blasted to bits.</P>
<P>Then, the Navy claimed that by decimating the bird population, they were actually helping Frew get more enjoyment from his hobby. "In some respects, bird-watchers get more enjoyment spotting a rare bird than they do spotting a common one," said a Navy lawyer.</P>
<P>So, according to the Navy, the more birds the Navy kills, the more enjoyment Frew will get from seeing the ones that are left alive.</P>
<P>--<I>Washington Post,</I> March 29, 2002</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Just say "no" to this Republican</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SARASOTA, FLA. resident Chris Hill was recently named one of the country's top 500 businessmen of the year by the National Republican Congressional Committee. That meant that he was also in the running for the GOP committee's top honor, "Republican of the Year."</P>
<P>That is, until the party found out that Hill is facing up to 20 years in federal prison for distributing drug paraphernalia. Hill's business apparently included manufacturing the kind of pipes commonly used for smoking marijuana.</P>
<P>When Iowa prosecutors raided several stores and found Hill's pipes, they charged him with the maximum, under the same "tough on drug" laws that Republicans seem so fond of--as long as it's poor Black or Latino teenagers who get caught.</P>
<P>Some might see Hill's arrest as "poetic justice," but Hill can't seem to see the humor. "I'm embarrassed that my government has put me in this position: they've lumped me in with some liberal longhaired dopers," he told reporters. "That's not the kind of crowd I run with."</P>
<P>--<I>News-Journal</I> wire service, March 2, 2002</P>

