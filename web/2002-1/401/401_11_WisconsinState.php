<P><font face="Times New Roman, Times, serif" size="5"><b>State of Wisconsin</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mike McHugh and Neil Loehlein</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 5, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MADISON, Wis.--"Enough is enough!" shouted more than 1,000 state workers who were protesting the proposed state budget cuts at the Wisconsin State Capitol on March 28. The rally was sponsored by AFSCME Council 24 and the Wisconsin Federation of Teachers (WFT).</P>
<P>"We are drawing a line in the sand today," said Marty Biel, Executive Director of AFSCME Council 24. "We will not be anybody's scapegoat!"</P>
<P>The rally was called to protest the budget cuts that have been proposed by state politicians to balance Wisconsin's $1.117 billion budget deficit. Assembly Republicans have pushed through a bill that will cut state retiree health care benefits, raise health insurance costs for current state employees and result in layoffs and wage freezes for state workers.</P>
<P>This budget will also affect public education and public services. The University of Wisconsin system will be forced to raise tuition for next year's enrolled students. Wisconsin cities and towns will also have to deal with cuts to the shared revenue program.</P>
<P>These budget cuts could be avoided by eliminating corporate tax breaks and subsidies as well as reinstating the progressive income tax.</P>
<P>"Corporate interests and the proliferation of corporate welfare are the problem," said WFT President Bob Beglinger. "We struggle with the costs of feeding our families and sending our kids to school."</P>
<P>The fact that Council 24 called their first rally since 1986 shows the pressure coming from angry rank-and-file workers. "While one rally won't turn Council 24 into a fighting union, it may signify a breakdown of the labor-management cooperation that got us into this mess in the first place," said AFSCME Local 171 President Mark Thomas.</P>
<P>Despite the militant sentiments of the crowd, the speakers concentrated on the November elections to pressure incumbent politicians to stand against the budget cuts. Rank-and-file activists are taking an approach to the current budget situation by organizing a rally against the budget cuts on May 1, International Workers' Day. This rally will bring together students, community groups and state and city workers, who are all affected by the current budget proposal.</P>
