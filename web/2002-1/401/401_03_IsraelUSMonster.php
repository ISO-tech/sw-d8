<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">U.S. bears responsibility for Israel's savage crackdown</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The monster that Washington created</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 5, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WHEN REPORTERS asked George W. Bush about the solution to the Israel-Palestine conflict on Monday, he sputtered: "There needs to be a focused coalition effort in the region against peace--I mean, against terror, for peace."</P>
<P>Bush was more truthful the first time. The "focused coalition" in the Middle East that Washington really wants is for war.</P>
<P>After declaring "victory" by restoring warlords to power in Afghanistan, Bush last month sent Vice President Dick Cheney to the Middle East to build support among Arab governments for Phase Two of the U.S. government's "war against terror." Target: Iraq. But Israel's onslaught against Palestinians has gotten in the way.</P>
<P>Not that Washington cares about justice for the Palestinians. The White House has repeatedly denounced Yasser Arafat, even as Israeli Prime Minister Ariel Sharon has stepped up attacks.</P>
<P>Meanwhile, the Arab monarchies and dictatorships of the Middle East want the "Palestinian problem" to go away. That's why they backed a Saudi Arabian "peace plan" to create a powerless Palestinian state without giving refugees the right of return.</P>
<P>But the scale of Israel's assault forced the Arab governments to denounce Israel. The struggle in Palestine is so popular in Arab and Islamic countries because it is at the heart of the fight against imperialism in the Middle East.</P>
<P>This is why the Bush gang has vacillated in recent days--from declaring that Israel has the "right to defend itself" to supporting a United Nations resolution calling for a pullback of Israeli forces from Yasser Arafat's Ramallah compound.</P>
<P>"So I guess I'm confused," one reporter asked White House press secretary Ari Fleischer at a press conference Monday. "Which is it, do we want Israel to withdraw, or do we support what Sharon is doing?"</P>
<P>Washington is split between those who want to allow Israel to drown Palestinian resistance in blood and others who believe that it's necessary to revive the "Middle East peace process" before starting a new war process against Iraq.</P>
<P>But whatever the day-by-day twists, both sides in Washington's debate are determined to make sure that U.S. interests are served in the Middle East. This means that Israel will remain the top recipient of U.S. military aid--and act as Washington's watchdog in the region. If the dog is off the leash now, the master has only himself to blame.</P>
<P>What's more, Israel is only applying Washington's own approach from the "war against terrorism": Use military power to impose our will, no matter what the consequences.</P>
<P>The <I>New Yorker</I> magazine ran an article last week by Nicholas Lemann on "the White House hawks and their new strategy for global dominance," including the use of tactical nuclear weapons. Government officials view September 11 as a "transformative moment," Lemann wrote, because it "drastically reduced the American public's usual resistance to American military involvement overseas, at least for a while."</P>
<P>"Is the United States now in a position to be redrawing regional maps, especially in the Middle East, and replacing governments by force? Nobody thought that the Bush administration would be thinking in such ambitious terms, but plainly it is, and with the internal debate to the right of where it was only a few months ago."</P>
<P>We can't let Bush pose as a peacemaker while he prepares for an all-out war on Iraq, a nation still in rubble after the 1991 Gulf War and a decade of murderous sanctions. The antiwar demonstrations in Washington April 19-22 provide an important opportunity to make these connections.</P>
<P>People have taken to the streets across the U.S. in outrage at Israel's war on Palestinians. They can become part of the movement to stop Washington's war drive--along with others who have come to question Bush's effort to spread the U.S. war around the globe.</P>
<P>We need the biggest possible turnout in Washington to demand a cutoff of U.S. aid to Israel--and to tell Bush that we won't stand by as the U.S. widens its war.</P>

