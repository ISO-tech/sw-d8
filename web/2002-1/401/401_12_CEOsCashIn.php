<P><font face="Times New Roman, Times, serif" size="4">Jobs and benefits slashed while the CEOs cash in</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"Where am I going to work?"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 5, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE 20,000 workers who got pink slips from United Airlines last fall are scrambling to make ends meet. "We have people who have been looking for jobs since they were laid off," said Jennifer Biddle, a mechanic and union shop steward at United's San Francisco maintenance hub. "There's a certain amount of desperation."</P>
<P>But one person who lost his job at United doesn't have to worry. He's James Goodwin, the former CEO who was forced to resign in disgrace--but still got a $5.7 million severance package. United management announced Goodwin's golden-parachute deal last week--even as it continued to demand that workers take concessions.</P>
<P>And Goodwin is far from the only CEO who got rich while workers lost their jobs and life's savings. Former LTV Steel CEO William Bricker got a $1 million "retention bonus" last year--after the company filed for bankruptcy--on top of his $700,000-a-year salary.</P>
<P>Even after he quit last December, Bricker still got the balance of the bonus that was supposed to keep him at LTV--some $650,000. Yet when LTV liquidated last month, it cut off health care benefits to 80,000 retirees--and now their union-funded extension expired April 1.</P>
<P>"We should have had this for the rest of our lives," James Gilliam, who worked at LTV in Pittsburgh for 35 years, told a reporter. "They're messing us over, treating us like animals."</P>
<P>For the retirees, maintaining coverage under the federal COBRA law will cost about $1,000 a month--far too expensive for most retirees, many of whom have severe health problems resulting from decades of working with toxic materials. And those younger than 65 are ineligible for Medicare.</P>
<P>"I could get a job, but where am I going to work?" asked 61-year-old Carol McMahon, who lives with her husband on $19,900 a year from his LTV pension. "I have no education like the young girls today. I'd have to scrub floors or work in a McDonald's."</P>
<P>LTV workers' pensions will be covered by the federal Pension Benefit Guarantee Corp.--but benefits will be far below what the company had agreed to in union contracts.</P>
<P>The United Steel Workers of America had pressed George W. Bush to agree to cover medical and pension costs for steelworker retirees at the same time as he imposed tariffs on steel imports last month. Instead, Bush bailed out the steel companies--and told retirees to go to hell.</P>
<P>The new owners of LTV's mills, known as ISG, won't have to pay a dime to retirees--and plan to rehire only about half of the laid-off workers for lower pay.</P>
<P>The CEO looting at United and LTV is par for the course in Corporate America. Consider Gary Winnick, boss of the bankrupt telecommunications company Global Crossing. He sold $734 million worth of stock before the company collapsed--enough to buy a $92 million estate in the exclusive Bel-Air section of Los Angeles.</P>
<P>And despite scandals over how Global Crossing enriched Democratic politicians and ripped off investors, Winnick went ahead with a $15 million renovation plan on his house. Meanwhile, Global Crossing workers watched their jobs disappear and their retirement plans evaporate.</P>
<P>Another telecom tycoon, Philip Anschutz of Qwest Communications, recently sold $2 billion worth of stock. Meanwhile, 7,000 Qwest workers are being laid off--and the company may soon default on its debts.</P>
<P>Then there's WorldCom CEO Bernard Ebbers, who borrowed $374 million from his company and used the money to buy a 164,000-acre ranch in British Columbia two years ago. Last year, the nonunion company announced 10,000 layoffs--and thousands more job cuts are expected.</P>
<P>For all the talk of an economic recovery, it's so hard to find a job that the labor force participation rate is at a 40-year low. That's why the recent extension of unemployment benefits from 26 to 39 weeks by Congress won't solve the problem.</P>
<P>In most states, benefits are so low that the jobless quickly fall below the poverty line. And that's for workers who qualify for unemployment benefits--more than half the workforce doesn't.</P>
<P>Workers who were shut out of the 1990s boom have been shafted during recession and "recovery" alike. It's time to take a stand against corporate greed--and demand what's rightfully ours.</P>

