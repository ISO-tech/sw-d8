<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Supreme Court rules against rights of illegal immigrants</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>An attack on all workers</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 5, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ILLEGAL IMMIGRANTS don't count as human beings. That's the message that the U.S. Supreme Court sent last week when it ruled in a 5-4 vote that illegal immigrants aren't entitled to legal protection when they are wrongly fired from their jobs.</P>
<P>The Court heard the case of Jos&eacute; Castro, who was dismissed in 1989 for handing out union cards at a plastics company in California. Federal labor laws protect workers from being fired for trying to unionize, so the National Labor Relations Board awarded Castro $67,000 in back pay.</P>
<P>Not so fast, said the Supreme Court. The law only applies to workers if they're "legal." Since Jos&eacute; is an illegal immigrant, he wasn't entitled to compensation. Allowing the law to protect undocumented workers would "encourage" illegal immigration, said the justices.</P>
<P>But by not giving illegal immigrants the same protection from job harassment and discrimination, the ruling throws the most vulnerable workers in the U.S. to the wolves. The decision is a cruel attack on the estimated 7 million undocumented workers in the U.S. who usually toil in the lowest-paid and most degrading jobs.</P>
<P>It's also a slap in the face to unions like the Service Employees International Union and United Farm Workers (UFW), which have spent decades organizing immigrant workers, and to the AFL-CIO as a whole, which last year called for amnesty for undocumented workers. As UFW President Arturo Rodriguez put it, the court's decision will create "a permanent underclass of semi-slave laborers."</P>
<P>The ruling shows how the Bush administration's anti-immigrant scapegoating since September 11 dovetails with the interests of employers--and how far the Supreme Court will go in gutting the rights of all working people.</P>
<P>That's why an attack on immigrant workers is an attack on all workers--and why we must all stand together to defend immigrant rights.</P>

