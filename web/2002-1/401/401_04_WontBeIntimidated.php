<P><font face="Times New Roman, Times, serif" size="5"><b>We won't be intimidated or silenced by police</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 5, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>We're members of the ISO in Albany, Ga., a very conservative area in southwest Georgia. We found out just how conservative after our meeting last week.</P>
<P>When several members arrived home, we discovered that our house had been broken into. There was a note on the table with "BURN IN HELL" written in block letters. Nothing was taken or broken--everything else was just as we had left it earlier in the evening.</P>
<P>It turns out that an ISO supporter's boyfriend, who is also an activist, also has been harassed. The cops showed up at his apartment banging on the door and saying that there had been a call from his number.</P>
<P>Meanwhile, for the past two weeks, a woman has shown up at our weekly ISO meetings and acted suspiciously. She began to bait people and tried to get members to talk about things like gambling and drug running.</P>
<P>She showed up again this week with a lot of the same talk, and one comrade noticed that she was carrying a small tape recorder and recording the meeting. She began prying for people's whole names and contact information. We concluded that she most likely is a cop.</P>
<P>These are typical intimidation tactics that the authorities use against people when they try to fight back. But we aren't going to let them get away with it.</P>
<B><P>Jim T.,</B> Albany, Ga.</P>

