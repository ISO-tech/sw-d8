<P><font face="Times New Roman, Times, serif" size="4">Plea from a prisoner held at Guant&aacute;namo Bay:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"Can you tell the world about us?"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 5, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>FOR MONTHS, Abdul Hakeem Bukhari's family couldn't find him. A Saudi Arabian carpet trader, Bukhari disappeared after traveling to Pakistan in November to buy Afghan carpets.</P>
<P>Turns out that U.S. intelligence sources suspected him of terrorist sympathies. So in December he was arrested and shipped to Camp X-Ray, the U.S. prison camp in Guant&aacute;namo Bay, Cuba.</P>
<P>Bukhari's family finally received a letter from him in March. "At least now we know that he's still alive," his brother Abdul Rahman told the <I>Arab News</I>. But that's all they know about the conditions that Bukhari faces along with some 300 other detainees.</P>
<P>When journalists toured the facility in March, one prisoner tried desperately to attract their attention. "We've been on a hunger strike for 14 days, and nobody cares," he called out. "Can you tell the world about us?"</P>
<P>But Brig. Gen. Michael Lehnert, the commander of the camp, claims that it was just an act. "What the detainees want is a reaction," he told reporters. "They would like to be able to demonstrate, 'Look what's happening to me! I'm being punished.'"</P>
<P>But just to be safe, Lehnert explained, the military might bar reporters from seeing detainees. "[We] may end up moving you back so that you can no longer provide the catalyst for them to do those things," he said.</P>
<P>Detainees began a hunger strike in March to protest the degrading conditions and their uncertain futures. U.S. officials even announced last month that it might decide to hold prisoners indefinitely, even if they're cleared by military tribunals. Now the Pentagon wants to move 250 more prisoners to the camp.</P>
<P>This is the sick reality for the prisoners of Bush's "war on terrorism."</P>

