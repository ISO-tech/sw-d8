<P><font face="Times New Roman, Times, serif" size="5"><b>Past struggles with lessons for today</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Brian Belknap</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 5, 2002 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>BOOKS: </B>Howard Zinn, Dana Frank and Robin D.G. Kelley, <B><I>Three Strikes: Musicians, Salesgirls, and the Fighting Spirit of Labor's Last Century. </B></I>Beacon Press, 2001, 174 pages, $23.</P></font>
<font face="Times New Roman, Times, serif" size="3"><I><P>THREE STRIKES </I>is a wonderful book that recounts three important labor struggles from the first half of the century.</P>
<P>The first chapter, by activist-historian Howard Zinn, deals with the struggle of 11,000 miners to organize the Colorado coal fields in the early teens. Zinn brilliantly describes the strike that pitted the fledgling mineworkers union against Nelson Rockefeller and his Colorado Fuel &amp; Iron (CF&amp;I) Co. Zinn shows how completely CF&amp;I controlled the state government--and the hard lessons that miners learned about whose side the state was really on.</P>
<P>After numerous deadly confrontations with company goons, miners were initially relieved when the governor sent in the National Guard. "[B]ut soon, the same people who had wept with relief at the sight of the National Guard were bitterly mourning their dead and praying for deliverance," writes Zinn.</P>
<P>The worst incident of all became known as the Ludlow Massacre--as guardsmen machine-gunned the workers' encampment, doused the tents in coal oil and set them on fire. In Zinn's words, "The tents became crackling torches, and for hours, the countryside was aglow with a ghastly light." In a pit under one of the tents, the charred remains of 11 children and 2 women were found.</P>
<P>The miners lost their struggle, but Zinn rightly points out that the brutality used to defeat the strike was seared into workers' consciousness and fueled fightbacks years later.</P>
<P>Also in this book, Dana Frank tells the story of a 1937 sit-down strike at a Woolworth store in Detroit. At the time, Woolworth was a huge multinational that employed mostly young women--some as young as 14 years old--for 25 cents an hour or less. "It was like striking McDonalds, The Gap and Wal-Mart all at the same time," writes Frank.</P>
<P>Frank puts the women's fight in the context of the wave of sit-down strikes that then swept the country. "Every day, they would have heard tales of daring actions from neighbors and friends...every day they would have more time to think about the subsequent gains workers in Detroit were winning with their strikes," Frank writes. "And every day, they would have wondered if they could do it, too."</P>
<P>From relating the sexism that women faced to describing how they organized to meet their daily needs as the strike continued, Frank captures the spirit of this inspirational struggle.</P>
<P>Robin D.G. Kelley's final chapter on the struggle of musicians is less interesting, but overall, this book is well worth reading for a retelling of important fightbacks of the past--struggles that hold valuable lessons for the future.</P>

