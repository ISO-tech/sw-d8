<P><font face="Times New Roman, Times, serif" size="4">D.C. families don't have a decent place to live</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>This housing crisis kills</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 5, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>The Washington metro area is facing a housing crisis that is leaving thousands of families without a decent place to live. In Fairfax, Va., the average rent went up 40 percent in the last four years. In one county in Maryland, the average rent is $1,030 per month, far out of reach for most working families.</P>
<P>This situation doesn't just increase homelessness. It kills. Last January, the Herrera family--Israel, Alba and their children, Dayrisha and Caleb--were killed when a fire struck their row house. They died of smoke inhalation because they were unable to get out of the basement where all four lived.</P>
<P>The Herreras had been forced to "double up," sharing a single-family house with more than one family. The other family upstairs was able to escape to safety, but because of faulty smoke detectors and no outside exit, the Herreras weren't as lucky.</P>
<P>Initially, county officials responded by targeting negligent landlords who don't keep up their properties. While that can often be the case, the problem is really that the Herreras were forced to live in that basement at all--because they simply couldn't afford anything else. Now, three months after their deaths, officials are finally admitting that the housing shortage is really to blame.</P>
<P>These tragedies make it perfectly clear--the rich were the ones who benefited from the 1990s boom, and they will be the ones who benefit from any so-called recovery from the recession.</P>
<P>We have to fight for the housing and living conditions that we need.</P>
<B><P>Jeff Bale,</B> Washington, D.C.</P>

