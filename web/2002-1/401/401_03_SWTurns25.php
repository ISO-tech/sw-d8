<font face="Times New Roman, Times, serif" size="5"><b><I>SW</I> turns 25</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 5, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><I><P>SOCIALIST WORKER</I> turns 25 years old this month. That's a quarter century of reporting on struggles throughout U.S. society--the stories of working people that don't make it into the mainstream media.</P>
<P>A quarter century of giving the socialist analysis of a changing world--from crisis and war to resistance and revolution. A quarter century of upholding the history of the workers' movement around the world and putting forward the vision of a socialist future.</P>
<P>We've grown over the past 25 years--from a monthly to a weekly publication, with a circulation many times larger than when we began. For that, we have our readers to thank.</P>
<I><P>SW</I> isn't like the mainstream media. We rely on volunteer correspondents to tell their own stories in reports and articles, and our paper is distributed by people directly involved in the fight for a better world, in their workplaces, communities and campuses.</P>
<P>Turning 25 is an exciting milestone, and one that we're proud to celebrate. We have a series of features planned for this month--to tell the history of <I>SW</I> over the years, to ask our readers about how they use the paper and to talk about the challenges ahead.</P>
<P>Thanks to the hard work of hundreds of people, <I>SW</I> has been able to provide the critical information that our side needs--and become a forum for discussing the struggles we face.</P>
<P>But more than that, our paper can be a vital, living link that ties activists together into an opposition that can put socialist ideas into action.</P>
<P>We want to grow even faster in the coming months and years--and for this we need your financial help as well. We ask you to give generously. And we look forward to the years and struggles ahead.</P>
