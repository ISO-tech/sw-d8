<P><font face="Times New Roman, Times, serif" size="5"><b>They say the recession's over?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 5, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>I am an unemployed white-collar worker currently living in Northern California. As someone who has been laid off--<I>twice</I>--in the past year, I get really burned when I hear politicians saying that the recession "never really happened."</P>
<P>Although the second time I was laid off happened after September 11 and was attributed to that tragedy, the fact of the matter is that both of my jobs were cut because the dot.com bubble burst.</P>
<P>I get equally steamed by pundits who say that the recession is over now and that "everything is fine." If everything is fine, then explain this to me: About a month ago I applied for a job summarizing court depositions. It's a job so totally obscure and specialized that I figured no one else would apply for it. When I called the company to see what was up, Human Resources explained that 500 people had applied for three openings.</P>
<P>No recession? Recession over? Nonsense. My question is, what can mostly unorganized, unemployed workers do to fight back?</P>
<B><P>Elizabeth Terzakis,</B> Oakland, Calif.</P>

