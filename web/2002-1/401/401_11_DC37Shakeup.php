<P><font face="Times New Roman, Times, serif" size="4">New DC 37 leaders still silent on contract showdown</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>AFSCME shake-up in NYC</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Thomas Barton, member, AFSCME Local 768</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 5, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--As the contract between 135,000 AFSCME District Council 37 union members and the New York City government nears its June 30 deadline, the uproar in the union grows more intense.</P>
<P>Under pressure from leaders of DC 37's most powerful locals, DC 37's appointed administrator, Lee Saunders, last month was replaced by an elected leader, Lillian Roberts, who fought to build public employee unions during the 1960s and 1970s.</P>
<P>Since 1998, DC 37 has been under the control of Saunders, following a massive scandal that saw about half of the vice presidents indicted for corruption. The scandal came to light after top union officials stuffed the ballot boxes in 1995 to win approval of a five-year contract with two years of wage freezes.</P>
<P>Roberts, called out of retirement at age 76, has been put in office as a temporary caretaker in an executive council vote by a few powerful union leaders. Since leaving the union, she has served as New York State Labor Commissioner and an executive at an HMO.</P>
<P>Roberts was the compromise candidate between the old guard, allied with ousted DC 37 Executive Director Stanley Hill, and the reformers, led by Mark Rosenthal, president of Motor Vehicles Operators Local 983.</P>
<P>But as <I>Labor Notes</I> magazine pointed out, "The new DC 37 officers will receive the same inflated salaries as those dismissed in shame." Rosenthal, for example, will be paid $180,000 as DC 37 Treasurer in addition to the $38,000 he receives as a local president. Roberts will be paid $250,000 per year. Now the wheeling and dealing to see who will come after Roberts gets going behind the scenes.</P>
<P>Also last month, a jury failed to convict Local 768 President Helen Greene, on trial over $2,000 of disputed Local 768 credit card bills. At the time of the indictment, Greene, a leader in the union's old guard, was positioning herself to run for head of DC 37. Putting on a militant robe, she had bitterly opposed the inclusion of merit pay in the new DC 37 contract approved in 2001, and had called for an illegal strike against the city, the only DC 37 local president to publicly do so.</P>
<P>When she was indicted, Local 768 passed a resolution stating, "This local goes on record as rejecting the interference of the District Attorney's Office in the internal affairs of the local."</P>
<P>The first test of the new DC 37 leadership is the contract--Mayor Michael Bloomberg is demanding major concessions. But the new leaders have had little to say to the membership about the fact that the contract expires soon.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Butler ousted in Local 420 election</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>JAMES BUTLER, for 30 years president of AFSCME Local 420, has apparently been ousted in a disputed election that is under appeal over 112 challenged ballots. Butler's challenger, Carmen Charles, a Local 420 vice president, claimed victory.</P>
<P>She had accused Butler of calling her a "stupid immigrant" and called him a "street brawler" for his picket lines and marches against former mayor Rudolph Giuliani.</P>
<P>Underneath the campaign rhetoric was a spreading rank-and-file rebellion against Butler's autocratic and greedy leadership style. People were fed up with his move to hike their union dues while paying himself $250,000 per year. He was also notorious for spending millions of dollars from the local treasury on a new headquarters building that has never been opened.</P>
<P>Local 420 is made up of the worst-paid city hospital workers who do the dirtiest jobs. They cook, wash dishes, scrub floors and toilets and take out the trash. Their workloads have doubled or tripled as thousands have been laid off or not replaced after retiring.</P>
<P>As Local 420 member Jeff Charles told <I>Socialist Worker,</I> "They're killing us here. If we don't do something, they'll squish us like a bug. Butler does nothing except try to get more money out of us to feed his fat face."</P>

