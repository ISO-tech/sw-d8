<P><font face="Times New Roman, Times, serif" size="4">S.F. cops' sick attack on four youths</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Standing up to police brutality</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Caroline Palawan and George Vouros</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 5, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>RESIDENTS OF the Bay View Hunters Point neighborhood in San Francisco are coming together to say no to police brutality.</P>
<P>This predominantly African American community is outraged at San Francisco cops who molested and brutalized four youths, aged 12 to 14, on Martin Luther King Day in January. As many as 30 police were on hand as officers held the two boys and two girls at gunpoint in front of their pleading parents and neighbors.</P>
<P>The cops beat 14-year-old Jerome Brown so savagely that a pool of blood was left behind on the sidewalk. Jerome suffered a concussion, a dislocated jaw, and bruises and lacerations on his face, ribs, wrists and shoulders. Despite this, police held him at the station house for more than an hour and a half and provided no medical attention.</P>
<P>Male cops also shamelessly searched the two girls, touching them on their breasts, buttocks, legs and between their thighs. The girls were screaming at the top of their lungs, but their parents were prevented from crossing the street by police who said that they would be shot if they tried to reach their daughters.</P>
<P>"They say innocent until proven guilty," said Susie McAllister, the mother of one of the two girls. "I don't think so, because on January 21, those kids weren't innocent until proven guilty. They were treated like they were guilty. And their only crime is that they were African American."</P>
<P>Police claim that they were responding to a call about two Black men in ski masks unloading a car with guns. But that doesn't explain why the cops attacked four youths--two of whom were girls--and why the assault continued even after no weapons were found.</P>
<P>Residents say that a couple of the cops have been put on desk duty because of complaints about the attacks. But the department has yet to apologize, give the names of the officers involved or release any police report of the incident. Meanwhile, parents say that the four kids are still afraid to leave their homes.</P>
<P>But parents and neighbors have united with community and anti-police brutality groups to speak out. "A lot of people don't stand up to them because they think they have to fight the cops by themselves," said Inell Manuel, the mother of the other girl who was attacked. But Inell told <I>Socialist Worker</I> that this has begun to change since she and her neighbors have taken a stand. "They tell us to keep on going, and whenever they have any problems, they seem to come to us now," Inell said. "They figure since we came out that everybody can do it."</P>
<P>Inell and the other families of the victims plan to confront the Police Commission at its next meeting on April 10. "It's not going to happen overnight, and we can't give up the fight," said Susie McAllister. "But people have to open their mouth and speak out. There's power in numbers. We have to come together and fight together--and as long as we don't, whatever they want to do, they will."</P>
<I><P>Protest at the next meeting of the Police Commission. Meet at 5 p.m. at 850 Bryant St.</P></I>

