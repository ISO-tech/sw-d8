<P><font face="Times New Roman, Times, serif" size="4">Thousands show their outrage at Israeli repression</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>We want justice for Palestinians!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Evan Kornfeld, Mischa Sogut and Brian Huseby</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 5, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THOUSANDS OF people across the country took to the streets March 30 to protest Israel's occupation of Palestinian land. The demonstrations were called to commemorate Palestinian Land Day--the day 26 years ago that Israeli soldiers shot dead six Palestinians protesting the confiscation of their land. But Israel's stepped-up war in Ramallah and elsewhere earlier in the week gave the events added urgency.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<B><P>Los Angeles </p></B>
<P> More than a thousand people marched through the streets of Westwood, and many passing motorists honked in support. Chanting "Hey, hey, ho, ho, the occupation has got to go" and "Free, free Palestine," the march was upbeat and noisy.</P>
<P>Although organizers had received threatening phone calls, only four or five pro-Israel counter-demonstrators showed up.</P>
<P>The march ended with a rally at the Federal Building, where speakers addressed the crowd. Michel Shehadeh, of the American-Arab Anti-Discrimination Committee (ADC), urged the crowd to "send a strong and clear message for the Palestinian people. They are fighting to end colonialism in Palestine&#133;Sharon brought in tanks, F-16 fighter planes, Apache helicopters, and still he could not defeat the Palestinian people."</P>
<P>Muhammad Nasser, an American Muslim, told of how the struggle of the Palestinians has set an example for oppressed peoples around the world. "The Palestinian people are giants rewriting history," said Nasser. "They are saying to the Arab governments, 'You mean nothing!'"</P>
<P>"Were it not for the U.S.," said Jim Lafferty of the National Lawyers Guild, "the war in the Middle East would have ended long ago&#133;It's time to recognize the Palestinian people as full human beings."</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<B><P>Washington, D.C.</p></B>
<P> About 1,500 activists of many faiths and ethnicities gathered to call for an end to the Israeli occupation.</P>
<P>Hussein Ibish of the ADC described the occupation as "a structure of daily violence," describing how Israeli violence is labeled "self-defense" in the mainstream media while Palestinian resistance is categorized as "terrorism."</P>
<P>Khalid Turaani, director of American Muslims for Jerusalem, explained that U.S. support sustains Israel's occupation. "The blood of Palestinians is as much on the hands of Bush as it is on Sharon's," said Turaani.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<B><P>Seattle</p></B>
<P> About 300 marched through the streets, carrying signs with messages such as "No peace process without justice" and "Stop U.S. aid for the Israeli occupation."</P>
<P>"It's impossible for any kind of peace process, let alone peace, to co-exist with a military occupation," said Lena Tuffaha from the ADC.</P>
<P>At the end of the event, marchers were invited to pass through a mock Israeli checkpoint. Those who did were handed a card informing them that they could pass freely if they were Israelis or foreign nationals.</P>
<P>However, if they were Palestinians trying to pass through without permission, they would be shot--as open fire regulations currently allow soldiers to shoot to kill even when their lives aren't threatened.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<B><P>Chicago</p></B>
<P> About 250 held candles, listened to speakers and chanted "Hey Sharon, what do you say, how many kids did you kill today?"</P>
<P>The event brought out all kinds of people opposed to Israel's occupation. A speaker from the Jewish group Not In My Name talked about the myths she was raised on--and how she came to look at Israel's seizure of Palestinian land with a sense of shame and outrage.</P>
<P>Two days later, an even larger demonstration of 1,500 again took to the streets and marched on the Israeli consulate.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<B><P>San Francisco</p></B>
<P> About 700 people marched, chanting "No justice, no peace, U.S. out of the Middle East!" The event was sponsored by the ADC, American Muslims for Global Peace and Justice, Students for Justice in Palestine and others.</P>
<P>Altogether, the events were an inspiring show of struggle in solidarity with the people of Palestine. But now is an urgent time--we have to build on these protests and keep up the pressure.</P>

