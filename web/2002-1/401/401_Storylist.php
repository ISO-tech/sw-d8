<P><font face="Times New Roman, Times, serif" size="2"><B>WHICH SIDE ARE YOU ON?</b></font><BR>
<A HREF="401_07_Reparations.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Confronting the crime of slavery in the U.S.</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">In 1865, the U.S. government promised every freed slave 40 acres of tillable land, known as the "40 Acres and a Mule Proclamation." That was the last time the U.S. government gave any thought to compensating African Americans for 300 years of slavery. The reparations movement is now forcing the political establishment to reconsider.</P>

<P><font face="Times New Roman, Times, serif" size="2"><B>THE MEANING OF MARXISM</b></font><BR>
