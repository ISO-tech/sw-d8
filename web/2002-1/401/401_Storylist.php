<P><font face="Times New Roman, Times, serif" size="3"><b>Supreme Court rules against rights of illegal immigrants</b></font><BR>
<A HREF="401_03_AntiImmigrantCourt.shtml"><font face="Times New Roman, Times, serif" size="4"><b>An attack on all workers</b></font></A><BR>
