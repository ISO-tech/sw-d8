<font face="Times New Roman, Times, serif" size="3">Israeli Prime Minister Ariel Sharon has declared an all-out war on Palestinians. And his first target was Palestinian Authority leader Yasser Arafat. </P>

<P><font face="Times New Roman, Times, serif" size="3"><b>Jobs and benefits slashed while the CEOs cash in</b></font><BR>
<A HREF="401_12_CEOsCashIn.shtml"><font face="Times New Roman, Times, serif" size="4"><b>"Where am I going to work?"</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">The 20,000 workers who got pink slips from United Airlines last fall are scrambling to make ends meet. But one person who lost his job at United doesn't have to worry. He's James Goodwin, the former CEO who was forced to resign in disgrace--but still got a $5.7 million severance package. </P>


<p><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>ISRAEL'S REIGN OF TERROR</b></font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>A call to action from besieged Ramallah:</b></font><BR>
<A HREF="401_06_TouficHaddad.shtml"><font face="Times New Roman, Times, serif" size="4"><b>"Raise your voices right now"</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">Toufic Haddad is a Palestinian living in Ramallah and co-editor of the left-wing journal <I>Between the Lines</I>. He spoke with <I>Socialist Worker</I> as Israeli soldiers besieged Yasser Arafat's compound not far from his home.</P>

<P><A HREF="401_06_IsraelsCruelReign.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Israel's cruel reign of terror</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">The U.S. mantra that Yasser Arafat "must make a 100 percent effort to fight terrorism" is a way of avoiding the main issue and the true source of violence--Israel's daily humiliation and intimidation of hundreds of thousands of Palestinians living under its military occupation.</P>

<P><A HREF="401_06_SuicideBombings.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Suicide attacks "borne of despair"</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">The Israeli government claims that Palestinian suicide bombings are proof of the fanaticism they must defend against. But in reality, these Palestinian attacks are a product of hopelessness and despair--in the face of Israel's military offensive to drown the Palestinian resistance in blood.</P>


<p><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>SPECIAL FEATURES</b></font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>A look inside Illinois' broken death penalty system</b></font><BR>
<A HREF="401_05_FaceOfDeathRow.shtml"><font face="Times New Roman, Times, serif" size="4"><b>The real face of death row</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">Illinois Gov. George Ryan--who two years ago bowed to growing pressure and called a halt on all executions--has said that he will review the cases of all 163 Illinois death row prisoners during his final months in office. What will he learn?</P>

<P><font face="Times New Roman, Times, serif" size="3"><b>The revolutionary legacy that the media covers up</b></font><BR>
<A HREF="401_08_MalcolmX.shtml"><font face="Times New Roman, Times, serif" size="4"><b>The real Malcolm X</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">The recent discovery of letters, diaries and notebooks belonging to Malcolm X has stirred new interest in the man and his politics. But establishment voices that treat Malcolm X as a saint today seek to bury his real legacy of revolutionary internationalism and anti-imperialism.</P>


