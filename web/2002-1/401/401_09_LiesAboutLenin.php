<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Why do they tell lies about Lenin?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 5, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE RUSSIAN revolutionary Lenin is perhaps the most misunderstood, maligned and lied-about figure in history. If you learn about him at all, you're likely to hear that Lenin was a violent conspirator and a fanatical dictator.</P>
<P>According to this version of history, Lenin was no better than the man who succeeded him in power in Russia--Joseph Stalin, the dictator of the ex-USSR who built his career on the bones of revolutionaries in Russia.</P>
<P>Yet at the end of his life in 1924, Lenin wrote a testament demanding Stalin's removal from power. It was a desperate attempt to stop the growing state bureaucracy that Lenin saw springing up around him.</P>
<P>The truth is that Lenin dedicated his life to one thing and one thing only--the establishment of a society free from all forms of oppression. And to achieve this, he focused his attention on building an organization of revolutionaries--primarily of workers--capable of achieving that goal.</P>
<P>Lenin's conception of revolutionary organization has been distorted beyond all recognition. In part, these distortions are the work of people who continued to look upon Russia as socialist after Stalin came to power. This required accepting the idea that a Leninist "vanguard" party demanded blind obedience. "The special feature of the Communist Party," wrote one Stalinist hack in the 1930s, "is its strictest discipline, i.e., the unconditional and exact observance by all members of the Party of all directives coming from their Party organizations."</P>
<P>Compare this to what Lenin said about the Bolsheviks: "The Russian Social-Democratic Labor Party is organized on democratic lines. This means that all the affairs of the Party are conducted, either directly, or through representatives, by all the members of the Party."</P>
<P>Lenin wanted all issues of theory and tactics to be debated "as openly, widely and freely as possible." "Freedom of discussion," he said. "Unity in action."</P>
<P>But for there to be real unity in action, there had to be strict discipline once a decision was reached. This is in keeping with the traditions of the workers' movement. After all, all workers have to strike when the majority decides to--even the minority that voted against striking.</P>
<P>Lenin argued for a "vanguard" party. By this, he didn't mean an organization that tells others what to do or a tiny group that declares itself a "vanguard" but has no followers in practice. For Lenin, the "vanguard" simply meant the most advanced elements in the struggle--those workers who could lead in the day-to-day struggles, but who also saw the need to guide the struggle toward the overturning of capitalism.</P>
<P>British Marxist Duncan Hallas described the Leninist party as "an organized layer of thousands of workers&#133;firmly rooted amongst their fellow workers and with a shared consciousness of the necessity for socialism and the way to achieve it." That is, in fact, a perfect description of the Bolshevik Party that Lenin and his contemporaries built in Russia.</P>
<P>The Bolsheviks were active in every arena of struggle--but always with the aim of bringing together every local struggle into a mighty movement against the autocratic regime of the tsar. And they did just that.</P>
<P>Lenin's party was by no means a band of conspirators pulling the strings of the revolution. "Understand, please, that before us after all is a victorious uprising of the proletariat," wrote an opponent of the Bolsheviks after the revolution. "Almost the entire proletariat supports Lenin and expects its social liberation from the uprising."</P>
<P>An organization like the one Lenin helped to lead doesn't exist in the U.S. It has to be built. And when we start to cut through the slander and lies, we find that Lenin has a lot to teach us about that.</P>
<P><I>This column originally appeared on June 23, 2000.</P></I>

