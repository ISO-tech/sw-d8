<P><font face="Times New Roman, Times, serif" size="5"><b>Suicide attacks "borne of despair"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 5, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>SUICIDE BOMBINGS carried out by Palestinians are the top item in every newspaper and television report about violence in the Middle East. The pictures of broken glass, twisted metal and blood-splattered floors are horrifying. That's why Israeli officials have cynically made certain that reporters have full access to these sites.</P>
<P>Meanwhile, journalists are usually barred from areas in the Occupied Territories where the IDF are carrying out missile attacks and house demolitions.</P>
<P>Israel's goal is for the media to portray the suicide bombings as proof of the "viciousness" of Palestinians. But this is propaganda.</P>
<P>In reality, the suicide bombings are a product of hopelessness and despair--in the face of Israel's military offensive to drown the Palestinian resistance in blood. The young Palestinian men and women who blow themselves up do so in the belief that all hope for justice has failed--and that this is the only way to lash back at their oppressors.</P>
<P>The suicide bombings haven't advanced the Palestinian resistance. Each time, they have provided the Israeli military with an excuse for cracking down more viciously. But anyone who wants to understand the conflict in the Middle East needs to recognize that the Israeli government's occupation and barbaric violence inevitably lead to these kinds of attacks.</P>
<I><P>Ha'aretz</I> columnist Gideon Levy, for example, accurately described how the IDF's rampages through refugee camps paved the way for the recent wave of suicide attacks. "An Israeli army megaphone called on all the men and male youths in Dheisheh refugee camp to come out of their homes," Levy wrote. "[B]etween 1,000 and 1,200 men from the camp and from the adjacent village of Artis came to the quarry, which became a stockade. They remained in the quarry until night, wearing only their underclothing, without food and without medicines for those who needed to take them."</P>
<P>"For them, it was a day of humiliation and torture, which they will not soon forget. Who among us can imagine enduring this kind of false arrest? Can we imagine our children, aged 14 and 15, being taken at dawn to a quarry, being tied up and then being humiliated for so many hours, even though they had done nothing wrong? Would we not develop a feeling of hatred and a desire for revenge against those who behaved in this way?"</P>
<P>Israel's escalation of its war on Palestinians will only create more desperation. As Israeli journalist Nahum Barnea concluded: "The terrorism of suicide bombings was borne of despair, and there is no military solution to despair."</P>

