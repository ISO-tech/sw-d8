<P><font face="Times New Roman, Times, serif" size="4">Right-wing fanatic picked for top AIDS position</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>New White House nut</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 1, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH managed to pick perhaps the single worst person imaginable to co-chair the Presidential Advisory Council on HIV/AIDS.</P>
<P>While in office, Rep. Tom Coburn, a practicing physician from Oklahoma, made a name for himself as one of the biggest right-wing fanatics in Congress. He opposes all abortion rights and led the fight to keep the abortion drug RU-486 from becoming legal. He's also anti-gay and pro-abstinence, insisting that sex outside of a heterosexual marriage is morally wrong.</P>
<P>According to one news report, Coburn was known among Capitol Hill interns for luring them with offers of free pizza to yearly lectures on abstinence--complete with graphic slides of people with sexually transmitted diseases. He repeatedly pushed for legislation to limit federal sex education funding to abstinence-only programs.</P>
<P>Coburn also promotes the idea that condoms don't protect against sexually transmitted diseases. In 1999, Coburn single-handedly held up legislation that would have helped uninsured women pay for cancer treatments--by attaching a clause that would have required the government to put warning labels on condoms saying that they were ineffective against some forms of sexually transmitted diseases. Most experts said that this would have led to an increase of unprotected sex.</P>
<P>Coburn's attitude toward AIDS education and funding is equally disgusting. His approach basically involves treating HIV and AIDS sufferers as criminals. Coburn favors "outing" HIV-positive people by reporting the names of anyone testing positive to public health authorities. He also advocates mandatory HIV testing for pregnant women.</P>
<P>While in office, he authored a bill that would have made the notification of sexual partners of HIV-positive people mandatory--and would have allowed health care workers to refuse to perform any invasive medical procedures until a patient had been tested for HIV.</P>
<P>"Anytime you're putting a conservative Republican in charge of a spending program that has to do with diseases, it's a scary thing," said Jeff Getty, a spokesperson for the Survive AIDS group. But even so, Coburn's appointment stands out--as proof positive of the Bush administration's right-wing fanaticism.</P>

