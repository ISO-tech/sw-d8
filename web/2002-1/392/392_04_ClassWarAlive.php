<P><font face="Times New Roman, Times, serif" size="5"><b>Class war is alive and well in Bush's America</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 1, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>Despite all the hype about "United We Stand," it's nice to know that the class war is alive and well in capitalist America.</P>
<P>Enron screwing its employees and retirees out of millions is the best-known example. But Polaroid did the same thing when, before going belly-up, executives awarded themselves millions of dollars in payments and benefits, while they slashed jobs and benefits for workers and retirees.</P>
<P>Not to be outdone, our wonderfully "bipartisan" Congress, at the urging of George W. Bush, passed $15 billion in aid to airlines last fall at the same time as the industry cut more than 100,000 workers.</P>
<P>Not one cent went to those workers! Instead, after some hand-wringing by craven liberals, our peerless wonders in Washington, D.C., extended unemployment benefits a few extra weeks. Thanks a lot, but only 39 percent of laid-off workers in the richest country on earth even qualify for unemployment insurance. "It's just too expensive," our leaders tell us, as they rain bombs on a hapless country half a world away!</P>
<P>And, by the way, don't worry about military tribunals, preventative detention, expanded police powers, racial profiling and lifting restrictions on the intelligence agencies. As Adolf Hitler said in an address to the German Reichstag on the occasion of the enactment of the Enabling Act in 1933: "The government will make use of these powers only insofar as they are essential for carrying out vitally necessary measures."</P>
<P>So there!</P>
<B><P>Roger Fraser,</B> Rolling Meadows, Ill.</P>

