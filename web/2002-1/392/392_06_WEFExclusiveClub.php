<P><font face="Times New Roman, Times, serif" size="5"><b>Exclusive club for bosses</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 1, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>ANTHONY ARNOVE looks inside the bosses' club known as the World Economic Forum (WEF).</P></B>
<P>"COMMITTED TO improving the state of the world." That's the slogan of the World Economic Forum. But a more honest one would be: "Committed to plundering the world for the superrich."</P>
<P>Since 1971, the WEF has organized an annual meeting for CEOs of the world's largest corporations, investors, politicians, bought-and-paid-for journalists and various academics. The forum is funded largely by 1,000 corporations that pay up to $300,000 to be members of the global elite club.</P>
<P>WEF regulars read like a Who's Who of global big business: Microsoft guru Bill Gates, Steve Case of AOL-Time Warner, media mogul Rupert Murdoch and GE's Jack Welch. The companies represented at the WEF "account for some 80 percent of global industrial output," says the <I>Financial Times</I> newspaper.</P>
<P>WEF members "control many of the world's governments and the bulk of its economic and military capabilities," adds the <I>Economist</I> magazine. "Although 40 or so heads of state will troop to [the WEF]&#133;the event is paid for by companies, and run in their interests. They do not go to butter up the politicians; it is the other way around."</P>
<P>Of course, a few players have retired--so to speak. One WEF regular of the past was Enron CEO Kenneth Lay, "a big wheel at the forum," according to the <I>Financial Times</I>. Last year, the <I>Irish Times</I> reported, Lay "held forth as a 'real economy' player on the lessons of the dot-com bubble." This year, he'll be too busy showing up in Houston courtrooms to defend his behavior at Enron--or so WEF organizers must have thought when they decided to scrub him from the agenda.</P>
<P>The WEF conference has long been held at the exclusive Swiss ski resort of Davos. But this year, the meeting was moved to New York City--out of "solidarity" with the city following the September 11 attacks.</P>
<P>In fact, attendees at last year's Davos conference faced a massive protest mobilized from across Europe. WEF organizers don't want a repeat--and are counting on New York cops to live up to their notorious reputation for cracking heads.</P>
<P>This year's meeting will be co-chaired by former New York Mayor Rudolph Giuliani and his billionaire successor Michael Bloomberg, a longtime WEF member. Bloomberg is planning to spend millions to contain protests against the WEF--even as he calls for even more cutbacks to a city budget that Giuliani already slashed to the bone.</P>
<P>New York has been hit hard by the recession--and hundreds of thousands of working people are in desperate need of help to put food on the table and to keep a roof over their heads. But Bloomberg is proving that his priorities are the same as the WEF's--protect the wealth and power of the rich at the expense of everyone else.</P>
<P>That's why thousands of protesters will propose a different agenda: New York rebuilt for people, not for profit.</P>

