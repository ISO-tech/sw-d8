<P><font face="Times New Roman, Times, serif" size="5"><b>Accuride workers defy UAW leaders</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 1, 2002 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>HENDERSON, Ky.--Members of United Auto Workers Local 2036 once again defied Accuride management and the UAW International to reject a union-busting contract by a vote of 170-87.</P>
<P>UAW officials last month cut off strike benefits and medical insurance to pressure workers--who went<I> </I>on strike and were locked out nearly four years ago. Terry Thurman, director of UAW Region 3, which covers Kentucky, told Local 2036 President Bill Priest that the local's charter also could be revoked unless workers voted for the contract.</P>
<P>But Accuride workers--many of whom have severe medical problems as the result of working in the wheel-making plant--still said no. Mary McGan, who will be unable to pay for surgery on her arm without medical coverage, called the deal a "yellow-dog contract," a miners' union term for management contracts with individual workers who agree not to join a union.</P>
<P>McGan, whose father and grandfather were miners, helped to organize the UAW into the plant in 1979 and heads the women's committee in Local 2036. "I've lost everything, my vehicle, everything but my house, which is a trailer," McGan told <I>Socialist Worker</I>.</P>
<P>The determination and grit of Local 2036 is why the UAW International chose former Local 2036 President Billy Robinson to be a temporary organizer at the nonunion Toyota truck assembly plant across the Ohio River in Indiana. But now, "what that International has done at Accuride has ruined the UAW in this area for years," Robinson said at the Local 2036 hall.</P>
<P>Although UAW International officials have spread the lie that Local 2036 members have given up, "the vote showed that there is still real interest here in negotiating a contract," Robinson said.</P>
<P>Local 2036 members plan to take their fight to the UAW convention in Las Vegas in June.</P>

