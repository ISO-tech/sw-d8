<p><a name="UMass"></a><font face="Times New Roman, Times, serif" size="4"><b>UMass Amherst</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul Waite, AFSCME Local 1776</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ADMINISTRATORS AT the University of Massachusetts Amherst announced 95 layoffs on January 18 as part of a plan to offset a $15 million budget shortfall.</P>
<P>The job cuts will hit across the university. For example, University Child Care--which has been in operation for 30 years--will close on May 25, except for flexible day care for 20 children.</P>
<P>Not only will 22 university employees lose their jobs, but 50 part-time student positions will be lost, and many families will have to find alternative child care arrangements in an area that is short of adequate facilities.</P>
<P>To add insult to injury, on the same day that the university announced the layoffs, it also revealed plans to build a new hotel-conference center and a new $63 million heating plant. Administrators have already hiked fees for undergraduate students by $495 for the spring semester.</P>
<P>It's time for workers and students to stand together against layoffs and fee increases.</P>

