<p><a name="workfare"></a><font face="Times New Roman, Times, serif" size="4"><b>New York workfare</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Amy Muldoon</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ABOUT 60 former welfare recipients and their supporters gathered on the steps of New York's City Hall to demand that Mayor Mike Bloomberg reverse the firing of 3,500 Parks Opportunity Program (POP) workers.</P>
<P>The workers were fired by outgoing Mayor Rudolph Giuliani, whose welfare chief Jason Turner negotiated a $578 million contract with TempForce to rehire the same workers for $1.50 less an hour with no benefits or job security.</P>
<P>TempForce will make a $75 million profit on the deal. "These people are profiteering off other people's misery," said Mark Rosenthal, president of Motor Vehicle Operators, Local 983, AFSCME DC 37. "We need a moratorium on kicking people off welfare. This isn't just a union issue, it's a social justice issue."</P>
<P>The POP program is one of the city's welfare-to-work programs. It placed workers in union jobs earning $9.38 an hour in the Parks Department for 11 months.</P>
<P>Protesters also demanded that Bloomberg create living-wage jobs for 10,000 people who have hit the five-year limit on welfare benefits--and provide jobs for 40,000 more people who will reach this limit. "They're taking money out of the pockets of these workers and putting it into the profiteer's pockets--literally taking food out of the mouths of their children," said Charles Ensley, president of Social Service Employees Union Local 371, DC 37.</P>

