<P><font face="Times New Roman, Times, serif" size="4"><b>Verizon</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By a shop steward, CWA Local 1109</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>RANK-AND-FILERS at Verizon are furious with the company's unilateral implementation and rapid execution of its Service Excellence Plan (SEP). SEP is an obvious attempt to challenge the strength of the union, fire thousands of workers and drastically increase the pace of work.</P>
<P>Field techs, especially in installation and repair, are being written up for below-average productivity. Any technician whose productivity falls below a monthly average can be progressively disciplined. Of course, there's no real way to rate performance--especially when the company is so cheap that it still relies on cables and infrastructure from the turn of last century!</P>
<P>So many union members are halfway to being fired that local Communications Workers of America presidents in the New York City area met to plan a response to the company's war on the union.</P>
<P>Union officials have proposed special training courses for stewards as a first step in a mass grievance campaign. But this won't save the jobs of those in danger.</P>
<P>A coordinated job action or strike is the only way to stop the company dead in its tracks. Union members should call on local leaders as well as the International to demand a united fightback to save our jobs.</P>
<P>We've struck and won before. We can do it again.</P>

