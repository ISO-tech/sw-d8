<p><a name="England"></a><font face="Times New Roman, Times, serif" size="4"><b>New England Gas Co.</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By John Osmand</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"JUDAS." THAT'S what Roberta, a locked-out worker at New England Gas Co. in Providence, R.I., calls Frank Devlin, a company vice president who used to be an official for United Steelworkers of America Local 12431.</P>
<P>About 250 Local 12431 workers were locked out at New England Gas starting on Martin Luther King Day. Union members refused to ratify a contract proposal that put profits before safety.</P>
<P>The company's offer would have allowed nonunion temporary workers to handle live natural gas pipelines. Last summer, one of around 25 untrained temp workers turned off the gas at the wrong house--and then turned it back on without lighting the pilot lights, allowing the house to fill to dangerous levels of explosive gas.</P>
<P>The company also wants the right to lay off any new hires since 1996 and anyone who isn't qualified for field work. The company didn't even offer a raise--only a cash bonus that management graciously doubled, from 1 to 2 percent.</P>
<P>Picketers say that they would like to see a labor rally at the State House, where they could come together with Providence teachers and workers at the <I>Providence Journal</I> newspaper, who are also in contract disputes.</P>
<P>Labor needs to show its strength to beat back the attack on workers' rights.</P>

