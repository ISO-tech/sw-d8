<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The death penalty: Too flawed to fix</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 1, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>JANUARY MARKED the 25th anniversary of the resumption of state-sponsored killing after a 10-year pause. A growing movement had exposed the death penalty as racist and arbitrary, leading to a virtual moratorium on executions by 1967.</P>
<P>In 1972, the U.S. Supreme Court ruled that existing laws governing the death penalty amounted to "cruel and unusual punishment"--and were unconstitutional. But four years later, the justices gave states the go-ahead to restart executions--as long as new laws conformed to supposedly fairer standards.</P>
<P>Gary Gilmore provided the forces of law and order with a golden opportunity to rehabilitate their death machine. A white ex-convict out on parole who confessed to two murders, Gilmore demanded execution and rejected efforts to stop it. On January 17, 1977, he was killed by a firing squad.</P>
<P>Since then, 752 more people have been murdered by the state. But "modern" death penalty laws have turned out to be just as racist and unfair as ever.</P>
<P>Some 35 percent of those executed since 1977 were Black, nearly three times the proportion of African Americans in the population. Of more than 3,700 people on death row today, 46 percent are Black.</P>
<P>Meanwhile, 99 people--some of whom came within days and even hours of being killed--have been proven innocent and freed from death row. This number begs the question: How many innocent people were put to death before justice could be done?</P>
<P>The exposure of outrages like these--along with years of organizing by death penalty opponents--pressured Illinois Republican Gov. George Ryan to declare a halt on executions two years ago. Resolutions calling for moratoriums passed in dozens of cities, ranging from liberal San Francisco to conservative Cincinnati. And for two years in a row, the number of executions dropped--a first since 1977.</P>
<P>Since September 11, the issue of the death penalty has been pushed off center stage. But according to a recent Gallup poll, support for the death penalty remains at a 20-year low.</P>
<P>It's time for opponents of the death penalty to step up the fight--and refocus attention on this unjust system. That's what activists will do January 31 in Chicago at a rally to mark the second anniversary of the Illinois moratorium--with a panel of speakers including Rev. Jesse Jackson and five exonerated death row prisoners.</P>
<P>Their message should go out to all the death penalty advocates who propose laws to "reform" the system--just as they did in the 1970s. The death penalty system is broken--and it's too flawed to fix. It's time to abolish the death penalty once and for all.</P>

