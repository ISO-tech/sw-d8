<P><font face="Times New Roman, Times, serif" size="4"><I>Village Voice </I>attacks protesters as terrorists</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>We won't let their smears stop us</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 1, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>Being an activist, you get used to attacks from right-wing crackpots and the media about global justice fighters being "middle-class students." But attacks from the once-upon-a-time left are always more disturbing.</P>
<P>When I picked up the latest copy of the <I>Village Voice </I>to check out their coverage of the upcoming anti-World Economic Forum protests, I got quite a shock. In an article titled "Law of the Fist," Richard Esposito writes, "Through the eyes of New York cops, the antiglobalization movement looks like one bloody line of terror and mayhem."</P>
<P>Protestors are terrorists? I didn't realize that the <I>Village Voice </I>sponsored a John Ashcroft sound-alike contest. He drips sympathy for "blue-collar" cops who aren't privileged enough to go to college and are, miraculously, mostly nonwhite.</P>
<P>Which magical NYPD is he describing? Not one I've ever seen. Not the one that tortured Abner Louima and butchered Amadou Diallo.</P>
<P>The police are getting ready to defend the world's richest pigs at the most glamorous trough, in a city where thousands of people will have no roof over their heads tonight. And I'm supposed to care about the cops?</P>
<P>Ironically, while Esposito takes great pains to paint a picture of students burning and looting their way across midtown, the tactics that police are preparing for tell another story. During training at Shea Stadium, police had lines of their cadets pretend to be protesters. They chanted, "No justice, no peace," and locked themselves down with linked arms. Hardly the maniacal pillaging that the <I>Voice</I> so cheerfully chose to focus on.</P>
<P>If the <I>Village Voice </I>wants to jump on the Homeland Security bandwagon--or get out front and pull it--that won't stop a movement based on fighting back against how the vast majority of us are exploited and oppressed.</P>
<B><P>Amy Muldoon, </B>CWA Local 1106, New York City</P>

