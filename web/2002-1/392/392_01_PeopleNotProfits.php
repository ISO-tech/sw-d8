<P><font face="Times New Roman, Times, serif" size="4">Money for jobs and schools, not for war!</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>People before profits</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 1, 2002 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WAR. RECESSION. World poverty that has left more than 1 billion people living on less than $1 a day. Those are just a few of "the big problems confronting humankind" to be discussed at the World Economic Forum (WEF) in New York City this month, according to Charles McLean, spokesperson for the organization.</P>
<P>But the people who suffer from the "big problems" aren't invited. Instead, 2,700 corporate chiefs and politicians will pay $25,000 each to attend the conference at the exclusive Waldorf-Astoria. They'll be guarded by thousands of riot cops stationed in a "frozen zone" where all protests are banned.</P>
<P>Those on the inside will enjoy the finest of everything. Billionaire brothers Steve and Robert Forbes will hold a "Friday Nightcap" party at the ritzy 21 Club, with a guest list that includes Bill Clinton and Hamid Karzai, the U.S. puppet ruler of Afghanistan. "The guests will feast on mini Maine crab cakes with curry-carrot dipping sauce, and will wash it all down with a Galet des Papes Vieilles Vignes Ch&acirc;teauneuf-du-Pape," the <I>New York Times</I> reported.</P>
<P>No doubt those who joined in the hunger riots in Argentina or who endure the misery of Afghanistan's refugee camps are grateful for such concern. And if the tens of thousands of New Yorkers laid off since September 11 won't get to mingle with the great and the powerful at the Four Seasons or Le Cirque, they can always stand in line at the city's booming soup kitchens.</P>
<P>The WEF--whose annual meeting was held until this year at the exclusive Swiss resort of Davos--tries to cultivate a liberal image by inviting a few union leaders and entertainers. But the real purpose of the WEF is to hatch business deals--and spread the gospel of corporate globalization. Behind the solemn talk about the world's "big problems," the corporate and political elite will be looking out for their own power and wealth.</P>
<P>But they won't go unchallenged. Thousands of people will participate in a weeklong series of protests, public forums, marches and direct actions.</P>
<P>New York politicians and media hacks have tried to intimidate demonstrators by claiming that they will be "dishonoring" the memory of those who died September 11 and playing into the hands of "the terrorists." The truth is that this gathering of global fat cats shows how corporate bosses and their political servants will stoop to anything to justify their system and the awful toll that it takes.</P>
<P>That's why we won't be silenced. We'll keep fighting for global justice--whether that means mobilizing in support of struggles around the world or defending the interests of working people in the U.S. or opposing the Pentagon war machine.</P>
<P>We're sending a message to the fat cats: Another world is possible--and we're building a movement that can bring it about.</P>

