<P><font face="Times New Roman, Times, serif" size="4">Bush is giving the green light to these killers</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>No to Israel's reign of terror!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 1, 2002 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE ISRAELI government took its reign of terror against Palestinians to a new level last week--and the Bush White House responded by giving it the green light.</P>
<P>Nearly every day, Israeli warplanes attacked security installations in territory supposedly controlled by Yasser Arafat's Palestinian Authority (PA). Missiles slammed into the security outpost next to Arafat's compound in Gaza, wounding seven people.</P>
<P>Arafat himself remains under siege in the West Bank town of Ramallah, with Israeli tanks dug in several dozen yards away from his headquarters, their barrels trained on the front door.</P>
<P>Meanwhile, Israeli forces continued their policy of "targeted killings"--in other words, cold-blooded assassinations of Palestinian leaders.</P>
<P>In a desperate attempt to strike back in any way possible, Palestinian suicide bombers carried out four attacks in Israeli cities in a span of 11 days. The Israeli government of Prime Minister Ariel Sharon used each attack as an excuse for even more violence--all supposedly in self-defense against "terrorist" attacks.</P>
<P>What hypocrisy! Israel's crackdown--carried out against Palestinians usually "armed" with nothing more than stones and slingshots--has been far more deadly. More than 800 Palestinians have been killed since the new Intifada began 16 months ago, and tens of thousands have been maimed or wounded.</P>
<P>But this hideous record didn't stop George W. Bush from dropping all pretense of being neutral last week--and attacking Arafat and the PA for causing the latest round of violence. "With talk of all-out war in the Holy Land, the Bush administration has granted Israel its widest military freedom of action since--in an ominous precedent--a Republican administration turned a blind eye to Ariel Sharon's 1982 invasion of Lebanon," wrote the Israeli newspaper <I>Ha'aretz</I> last week. In that war, Israel killed more than 17,500 Lebanese civilians, maimed hundreds of thousands and wrecked a whole country.</P>
<P>Even Arafat--who has made one concession after another to Sharon's demands for the arrest of Palestinian militants--responded to Israel's onslaught with defiance. "Give me the honor of being one of the martyrs for holy Jerusalem," Arafat declared in a speech, with the sound of gunfire from Israeli soldiers ringing in the background.</P>
<P>But PA security forces have continued to crack down on Palestinian militant groups like Hamas, leading to angry confrontations. In Nablus, a crowd of 2,000 protesters staged an angry rally outside a PA prison, forcing jailers to release the brother of a man killed by Israeli soldiers.</P>
<P>For their part, Israeli officials continue to insist that all of their repressive methods--forced closures of Palestinian areas, targeted killings, security checkpoints and house demolitions--are justified. When UN officials responsible for Palestinian refugees criticized the destruction of homes in Rafah that left 619 homeless in the dead of winter, Israeli officials claimed that the houses were "empty."</P>
<P>The violence has grown so horrific that even some Israel Defense Forces reservists went public with their opposition to the assault on Palestinians. "We will not continue to fight&#133;in order to rule, to expel, to destroy, to blockade, to assassinate, to starve and to humiliate an entire people," some 50 reservists wrote in a letter to Israeli newspapers. Since October 2000, more than 400 soldiers have refused to serve in the Occupied Territories.</P>
<P>Yet George Bush can't make a single criticism of stepped-up Israeli repression. Instead, Bush again called on Arafat to explain his connection to a ship carrying weapons allegedly bound for the PA that was intercepted by the Israeli Navy in early January. "Ordering up weapons that were intercepted on a boat headed for that part of the world is not part of fighting terror," Bush said. "That's enhancing terror."</P>
<P>Who does this man think he's fooling? Israeli forces daily carry out terrorism against Palestinians--using billions of dollars worth of weaponry that was bought and paid for by the U.S.</P>
<P>We have to oppose Israel's barbaric war on Palestinians--and demand that the U.S. cut off all aid for this reign of terror.</P>

