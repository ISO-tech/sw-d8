<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">What Bush won't talk about:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The real state of the union</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 1, 2002 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>A PROGRAM to "preserve and extend the great good that we've seen come out of the evil of September 11." That's how a senior administration official described what George W. Bush has planned for his State of the Union address on January 29.</P>
<P>The "great good" he's referring to, you ask? Astronomical defense spending and more tax cuts for the rich.</P>
<P>Bush is proposing a whopping 15 percent spending increase for the Pentagon--a $48 billion hike that will bring next year's budget up to $379 billion. That's more than the combined military spending of the <I>15 countries</I> with the next largest defense budgets.</P>
<P>And Bush isn't giving an inch on his "economic stimulus package"--a massive giveaway to corporations and the rich.</P>
<P>That's Bush's State of the Union--and it's completely out of touch with the real state of the union. During his address, Bush will parade New York City firefighters, cops and relatives of September 11 victims for the cameras. He'll probably recognize Afghanistan's handpicked interim leader, Hamid Karzai, to remind viewers of the U.S. government's war victory.</P>
<P>But Bush won't be recognizing Abdul Naim, who lives in the Afghan village of Rabat. Abdul watched in horror as his father tried to clear away unexploded bomblets from U.S. cluster bombs from a patch of land where he hoped to plant food--only to have one explode into pieces of shrapnel that tore open his throat.</P>
<P>When Bush pushes his stimulus package, he won't lead a standing ovation for Joe McCaughey, a homeless temp worker in Minneapolis who has to draw from a lottery each night in the hopes of getting one of the scarce beds in a city homeless shelter.</P>
<P>It's also unlikely that Bush will dedicate his State of the Union address to Mark Lindquist, who worked for Enron for eight years, only to be laid off by voice mail when the company went bankrupt.</P>
<P>Bush may mention Enron in one of those "I feel your pain, my mother-in-law lost $8,000" kind of way. But Bush certainly won't talk about how Enron CEO "Kenny Boy" Lay bankrolled his political career--or about the meetings that Lay had with Vice President Dick Cheney to "discuss" the administration's energy policy.</P>
<P>Because of the Enron scandal, millions of Americans recognize the Bush administration as craven servants of Corporate America. But that doesn't mean you should expect the Democrats to take the offensive.</P>
<P>Senate Majority Leader Tom Daschle (D-S.D.) has spent most of the new year criticizing Bush not for his deep connection to Enron or his new corporate welfare schemes--but for failing to balance the budget.</P>
<P>Then there's Sen. Joe Lieberman (D-Conn.), Al Gore's running mate in 2000, who's usually too busy justifying Bush's "war on terrorism" to spend much time challenging the White House.</P>
<P>The Democrats won't go after Bush because they're bathed in the same corporate cash as the Republicans. That's why it's up to us to expose the truth about Bush's rotten policies--here and abroad--and to organize a fighting opposition.</P>

