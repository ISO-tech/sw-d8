<P><font face="Times New Roman, Times, serif" size="5"><b>They think that kids should "sacrifice"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 1, 2002 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>Mike Bloomberg was recently inaugurated mayor of New York City, and he lost no time announcing his proposal for budget cuts which could devastate working people's lives.</P>
<P>He has asked the Board of Education to prepare to cut up to 10 percent of the budget--in a city which already has severely overcrowded, understaffed and underfunded schools.</P>
<P>This is an insult to the 1.1 million children in New York's public schools and to the 80,000 teachers who have been working without a contract or a raise for almost two years. Already, New York City teachers earn far less than their counterparts in surrounding school districts.</P>
<P>Schools in New York City simply cannot "do with less." We need more money for books, supplies, school repair and smaller class sizes.</P>
<P>It's time to tell Bloomberg that he cannot sacrifice education to protect big business.</P>
<B><P>A reader, </B>New York City</P>

