<P><font face="Times New Roman, Times, serif" size="4">Bush panel cooks up deal for United mechanics</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Why I'm voting no</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 1, 2002 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>MECHANICS AT United Airlines (UAL) are preparing to vote on whether to strike February 20 or accept a recommendation by a Presidential Emergency Board (PEB) for a contract settlement.</P>
<P>The PEB, appointed by President George W. Bush under the Railway Labor Act that governs airlines, recommended that United accept mechanics' demands for a big raise--and then demand concessions from all unions simultaneously a few months later.</P>
<P>To the surprise of leaders of the International Association of Machinists (IAM), which represents the mechanics, UAL management accepted the proposal. But some rank-and-file members aren't satisfied.</P>
<P>This article is based on a shop-floor leaflet by JENNIFER BIDDLE, a shop steward and member of the strike committee in IAM Local 1781 at United's San Francisco maintenance facility.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>ON THE surface, the PEB's recommendations seem alarmingly fair, even sympathetic. Economic parity with other airline workers. Mechanics should not shoulder concessions alone. Full retro pay. Receipt and dispatch [job titles] should be ours.</P>
<P>But dig a little deeper. We must ask ourselves: Have we been starved for so long that the mere thought of making enough money to pay off all the debts we incurred because we haven't had a raise in 10 years lulled us into believing United Airlines could ever have our interests at heart?</P>
<P>There must, after all, be a reason that United Airlines endorsed the PEB's recommendations. Let's never forget, UAL is the company that gave us concessions during the biggest boom in the history of capitalism.</P>
<P>It forced us into a six-day workweek, fired mechanics for doing their jobs under the guise of violating a temporary restraining order, has time clocks that suddenly and mysteriously dock more and more of our pay the closer we get to a strike&#133;just to name a few of the ways the company has thanked us for our sacrifices.</P>
<P>We heard a few months ago that the company wanted to give mechanics raises, then take them back. How ridiculous, we thought. Coincidence in light of the PEB's recommendation? Or perhaps the company's business strategists and corporate lobbyists were already hard at work, before the PEB was even a twinkle in Bush's eye.</P>
<P>Certainly there are other reasons to reject the PEB's recommendations--delayed retro pay, for instance. But the main reason we must reject it wholesale is quite simply because it gives UAL the opportunity to take back what should be ours to begin with.</P>
<P>We must reject concessions now and for the life of our contract. We must demand that the company honor ALL of its unions' contracts.</P>
<P>Solidarity counts. Pilots, flight attendants, mechanics, utility workers, ramp, stores and customer service representatives need to stick together. To divide and conquer is the oldest trick in the book&#133;and the easiest way for an employer to break a union.</P>
<P>Our fight is part of a wider struggle that unionized workers in this country face. If our unions are weak, corporations like United will more easily contract our work out to places like China, Mexico, Singapore and to nonunion facilities in the U.S. Fighting against concessions now will help ensure a future for us in aviation and a future for union labor.</P>
<P>Send a message loud and clear to United's executives, shareholders, the PEB, President Bush and Congress. Vote NO to the PEB's recommendations, NO to concessions and YES to strike.</P>

