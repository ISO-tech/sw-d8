<P><font face="Times New Roman, Times, serif" size="4"><b>Protest Cincinnati police violence</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Kerl</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MORE THAN 150 activists rallied in a Cincinnati suburb January 21 to protest a killer cop's first day at a new job.</P>
<P>Last April, Stephen Roach--then a Cincinnati cop--killed a young unarmed Black man, Timothy Thomas, sparking days of protests and rioting. Roach was acquitted of criminal charges, despite the fact that he changed his story about the shooting three times.</P>
<P>He eventually quit the force in Cincinnati. But Roach is now back patrolling the streets in the suburb of Evendale. The village is mostly white, but an increasing number of Blacks and other minorities have moved in.</P>
<P>In an attempt to avoid opposition, city officials used an emergency ordinance to rush through Roach's hiring. But residents turned out to council meetings to show their anger at the hiring.</P>
<P>And on January 21, anti-police brutality activists rallied outside the Evendale Municipal Complex--carrying bottles of bug spray and chanting "No Roaches in Evendale!"</P>

