<p><a name="detentions"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend the rights of Arabs and Muslims</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lucy Herschel</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MORE THAN 100 people rallied in New York City January 26 to protest the detention of Arabs and Muslims on unknown charges as part of the "war on terrorism."</P>
<P>The demonstration, held in front of the Metropolitan Federal Detention Center in the Sunset Park neighborhood of Brooklyn, was called by a coalition of antiwar, civil liberties and Arab and Muslim organizations.</P>
<P>Pakistani immigrant Uzma Naheed described her family's ordeal after government agents invaded her home soon after September 11 and took her husband into custody. For three and a half months, officials at the detention center insisted that Uzma's husband wasn't at the facility.</P>
<P>When she finally saw him, he was shackled hand and foot. "My husband has lived and worked here for 10 years," she told the crowd. "Before September 11, we had no idea that something like this could happen to us."</P>
<P>Naheed's two sons spoke to the crowd, saying that they're called "terrorists" in school and demanding that the officials responsible for detaining their father explain their actions.</P>
<P>Protesters vowed to return to the Brooklyn detention facility every Saturday until officials reveal the names of detainees and what they're charged with.</P>

