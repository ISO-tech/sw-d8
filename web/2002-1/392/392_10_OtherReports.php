<p><a name="NCantiwar"></a><font face="Times New Roman, Times, serif" size="4"><b>North Carolina antiwar protest</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sara Ward</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>HUNDREDS OF people from North Carolina and surrounding states showed up in the pouring rain in Raleigh, N.C., January 18 for an antiwar march and rally. After a two-mile march from Martin Luther King Garden to the State Capitol, some 500 people attended a rally at the Exploris Museum.</P>
<P>Rania Masri, of the Palestinian group Al-Awda, spoke about the continued racist assault on Arabs at home and abroad. Among the other speakers were David Potorti and Amber Amundson, relatives of victims of the September 11 attacks who have been touring the country to get out their message of opposition to the war. "I didn't want anyone else to feel what I'm feeling," said Amundson, whose husband died in the attack on the Pentagon. "That's been the driving force behind what we're doing."</P>
<P>Activists organized other antiwar events to build for the march and rally, including a teach-in in Greensboro and a demonstration at Elon University during a visit by former Israeli prime minister Ehud Barak.</P>

