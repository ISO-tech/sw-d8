<P><font face="Times New Roman, Times, serif" size="5"><b>California energy crisis was a hoax</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 1, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>LAST YEAR'S energy crisis in California was a hoax orchestrated by the power industry. That's the conclusion of a Foundation for Taxpayer and Consumer Rights study released in January, one year after the first rolling blackouts began to hit.</P>
<P>The crisis came four years after California legislators voted unanimously to "deregulate" the state's electricity system--requiring utility companies to buy wholesale supplies of electricity on the "free" market. National power giants like Enron, Duke and Reliant recognized the opportunity to make a killing--and jacked up prices through the roof.</P>
<P>The explanation from power bosses and politicians alike was that there was a shortage of electricity in late 2000 and 2001--even though California was using less than in previous years, when prices had remained stable.</P>
<P>In late spring, Gov. Gray Davis committed the state to spending at least $43 billion in expensive long-term contracts for energy over the next 20 years. Suddenly, the "crisis" ended. "When they stole as much as they thought they could get away with, the 'crisis' mysteriously disappeared--leaving the people of California stuck with the tab," write study authors Harvey Rosenfield and Doug Heller. "It wasn't a shortage, it was a shakedown."</P>
<P>The rip-off was huge. Between January and October of last year alone, California consumers overpaid for electricity by $8.5 billion--and are expected to overpay by at least another $20.5 billion over the next decade. And that doesn't take into account money looted from the state treasury to pay off the power bosses.</P>
<P>Rosenfield and Heller estimate that, all told, the 1996 deregulation law cost $71 billion--or about $2,100 for every man, woman and child in the state.</P>

