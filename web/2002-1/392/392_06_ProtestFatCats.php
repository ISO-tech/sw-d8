<P><font face="Times New Roman, Times, serif" size="5"><b>Protest the global fat cats in New York</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 1, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE FATTEST fat cats in the world are coming to New York City this week for the World Economic Forum (WEF). Microsoft mob boss Bill Gates will be there. So will General Electric job cutter Jack Welch.</P>
<P>George W. Bush can't make it because of a "scheduling conflict," but Secretary of State Colin Powell will represent the administration. Powell is sure to swell with pride during the Thursday night opening address--given by Hamid Karzai, the State Department's handpicked interim head of the new Afghan government, installed in power by U.S. bombs.</P>
<P>During five days of panels, workshops and speeches, corporate chiefs and political power brokers will declare their commitment to peace, justice and prosperity for all. Then they'll head for the bars and backrooms to make deals.</P>
<P>After all, the WEF offers "a unique club atmosphere which is very conducive to&#133;addressing key issues of global relevance or initiating new business contacts," as the forum's own Web site puts it.</P>
<P>And it's certainly clear who's not on the guest list at this club. WEF organizers are working with New York City officials to mobilize a massive show of force to protect the conference from protests. New York police say that the areas surrounding the Waldorf-Astoria hotel will be "frozen zones."</P>
<P>The cops showed off their technique in mid-January with practice maneuvers at Shea Stadium. "Officers with riot shields confronted demonstrators played by police cadets, watched by the city's new counter-terrorism chief, a retired Marine Corps general," the <I>Financial Times</I> reported.</P>
<P>These preparations have been accompanied by a smear campaign, designed to paint protesters as thugs bent on violence. "After September 11, anyone who thinks that violence is a legitimate form of protest certainly won't find anyone to agree with them," snarled WEF spokesperson Charles McLean.</P>
<P>But the real perpetrators of violence will be inside the Waldorf. Thousands of people are ready to show that they won't let McLean and his friends turn reality on its head. Activists are planning days of meetings, protests and direct action to expose the WEF.</P>
<P>Until a few weeks ago, the anti-WEF demonstrations looked like they would be limited to a few small direct actions. But the winding down of Bush's war on Afghanistan and new fears about the depth of the U.S. recession--plus the explosive impact of the uprising in Argentina as an example of resistance to free-market policies--reinvigorated activists.</P>
<P>The Another World Is Possible coalition, the umbrella group organizing anti-WEF activities, voted to hold a legal march in addition to direct action, which will give larger numbers of people an opportunity to make their voices heard. Plus the AFL-CIO announced plans for its own rally, focusing on the impact of the recession and the crimes of corporate globalization.</P>
<P>These activities can bring together people involved in a range of issues--from labor to Bush's "war on terrorism," from the environment to homelessness and poverty--and breathe new life into the global justice movement.</P>
<P>Above all, we can show that we won't be intimidated--that we'll raise our voices in protest against the world's fat cats.</P>

