<P><font face="Times New Roman, Times, serif" size="5"><b>Growing outrage over Camp X-Ray</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 1, 2002 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"TO BE in an 8-by-8-foot cell in beautiful, sunny Guant&aacute;namo Bay is not inhumane treatment." That's what Defense Secretary Donald Rumsfeld had the nerve to tell reporters last week. Rumsfeld was talking about alleged Taliban fighters, captured during the war in Afghanistan, who are in custody in exposed chain-link cages in the U.S. military prison in Cuba.</P>
<P>Photos of prisoners being forced to kneel on rocky ground with their hands and feet bound, blacked-out goggles over their eyes and masks over their noses and mouths have led to mounting international criticism, even from U.S. war allies like Britain. "[T]he cramped metal cages baking in tropical heat&#133;seemed to belong to another more brutal era," Britain's <I>Guardian</I> newspaper reported. "This is a sort of Caribbean gulag, and without a doubt, the scene before us would raise concern if it was being run by any other country."</P>
<P>The administration is being asked to treat the 158 as "prisoners of war." That would entitle them to certain rights under the Geneva convention. So far, though, the Bush administration has insisted on classifying them as "unlawful combatants"--which means they can be degraded and humiliated, interrogated repeatedly without lawyers, forcibly drugged, tried by military tribunals and ultimately sentenced to death.</P>
<P>Even Secretary of State Colin Powell thinks the administration is on thin ice. Though a veteran war criminal himself, Powell last week publicly called on Bush to grant prisoner-of-war status to the men in Guant&aacute;namo. According to the <I>Washington Post,</I> "Powell and the Joint Chiefs of Staff have privately expressed fears that U.S. Special Forces or spies who are captured in battle while dressed in civilian garb could be mistreated if the United States refuses formally to extend Geneva Conventions standing to the captives in Cuba."</P>
<P>But Powell looked likely to be overruled by Bush as <I>Socialist Worker</I> went to press. That'll make members of Congress happy. A bipartisan brigade of lawmakers returned from an "inspection" of the conditions at Camp X-Ray full of praise for the U.S. military. "Some [prisoners] pretended that we weren't there, but I think that we were all giving them the evil eye," said Rep. Ileana Ros-Lehtinen (R-Fla.).</P>

