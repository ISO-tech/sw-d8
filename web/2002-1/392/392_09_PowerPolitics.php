<P><font face="Times New Roman, Times, serif" size="5"><b>We need a fight for a better world</b></font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>BOOKS:</B> Arundhati Roy, <B><I>Power Politics,</B></I> revised edition. South End Press, 182 pages, 2001, $12.</P></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Amy Muldoon</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 1, 2002 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AS MANY of us fighting for social change today face attack, Arundhati Roy is a strong voice of defiance.</P>
<P>Roy's 1998 novel <I>The God of Small Things </I>sold more than 6 million copies in 40 languages and exposed caste and women's oppression in her native India. The Indian government charged her with "corrupting public morality" because it included a cross-caste love affair. Her activism against the displacement of lower-caste villages and writings on globalization further angered India's rulers, who framed her for conspiracy to kill court officers during a protest.</P>
<I><P>Power Politics</I> is a collection of her articles on globalization, free speech and, in the new edition, the U.S. war on terrorism.</P>
<P>The effects of globalization and class polarization in India are best illustrated in two industries: power and dam building. More than 70 percent of India's rural population has no electricity. To "solve" this problem, India's rulers negotiated with multinational corporations to build power plants.</P>
<P>The first foreign investor was scandal-ridden and now-bankrupt Enron. The electricity sold from Enron's plant in the $20 billion deal is seven times more expensive than the cheapest competitor in the same state. The local government found it cheaper to pay Enron not to produce electricity.</P>
<P>Similarly wasteful, but even more destructive, are the Big Dam projects. Sold as a solution to India's food insecurity, massive dams are being built at an alarming rate. For the rich, the dams are a jackpot--generating about $46 billion a year. For the poor, they're a nightmare--displacing as many as 56 million of India's poorest people as millions of acres of land are submerged.</P>
<P>In "The Algebra of Infinite Justice," Roy attacks the moral high ground Bush and Co. have crawled onto in the wake of September 11. "The equivocating distinction between civilization and savagery, between the 'massacre of innocent people' or, if you like, 'a clash of civilizations' and 'collateral damage.' The sophistry and fastidious algebra of infinite justice," writes Roy. "How many dead Iraqis will it take to make the world a better place? How many dead Afghans for every dead American?&#133;How many dead mojahedin for each dead investment banker?"</P>
<P>Roy has contributed to the antiglobalization movement, both for exposing<I> </I>the human cost in India and for making a case that the war is the "gunboat diplomacy" of the same interests whose "merciless economic agenda that has munched through the economies of poor countries like a cloud of locusts." Not only does Roy confirm why we need to fight for a better world, but she expresses optimism about getting there.</P>

