<P><font face="Times New Roman, Times, serif" size="5"><b>Protest the global fat cats in New York</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 1, 2002 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>The fattest fat cats in the world are coming to New York City for the World Economic Forum (WEF). In this special feature, <I>Socialist Worker</I> looks inside this exclusive club for the bosses, interviews leading global justice activists Dennis Brutus and Eric Toussaint and makes the case for an alternative to their sick system.</P>

<P><A HREF="392_06_ProtestFatCats.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Protest the global fat cats</b></font></A><br>
<font face="Times New Roman, Times, serif" size="3">Microsoft mob boss Bill Gates will be there. So will General Electric job cutter Jack Welch. George W. Bush can't make it because of a "scheduling conflict," but Secretary of State Colin Powell will represent the administration.</P>

<P><A HREF="392_06_DennisBrutus.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Brutus: "We're exposing how corporate power works"</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">"For the world at large, it's important to expose the WEF and its connection with the WTO and the others--to show how corporate power works," says Dennis Brutus, South African poet, anti-apartheid fighter and leading figure in the global justice movement. "When we get to the WEF, we've gone past the facade--we're really at the festering sore."</P>

<P><A HREF="392_06_EricToussaint.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Toussaint: "The IMF model is totally bankrupt"</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">"We have a crisis of neoliberalism in Argentina and on the world scale--the bankruptcy in Enron, the slowdown in the world economy and the massive offensive of capital against labor," explains Eric Toussaint, author of <I>Your Money or Your Life</I>.</P>

<P><A HREF="392_06_WEFExclusiveClub.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Exclusive club for bosses</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">"Committed to improving the state of the world." That's the slogan of the World Economic Forum. But a more honest one would be: "Committed to plundering the world for the superrich."</P>

<P><A HREF="392_06_AlternativeSystem.shtml"><font face="Times New Roman, Times, serif" size="4"><b>The alternative to their sick system</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">Tens of thousands of Argentinians took to the streets at the end of last week in the first major demonstrations since mass protests toppled two governments in December. The unrest showed the continued anger of Argentine workers and the poor at harsh free-market policies.</P></FONT></BODY>

