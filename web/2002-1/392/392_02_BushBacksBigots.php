<P><font face="Times New Roman, Times, serif" size="4">Anti-abortionists thrilled by Bush's rally speech</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Bush backs the bigots</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 1, 2002 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH just couldn't resist the urge to insult women across the country last week. On January 22--the 29th anniversary of the <I>Roe v. Wade</I> Supreme Court ruling that gave women the legal right to abortion--Bush spoke via telephone to a rally of thousands of anti-choice fanatics in Washington, D.C.</P>
<P>"You're working and marching on behalf of a noble cause and affirming a culture of life," Bush told the crowd. Bush's speech was warmly received by the bigots, some of whom had the gall to compare abortion with the September 11 terrorist attacks.</P>
<P>As Gloria Feldt, president of the Planned Parenthood Federation of America, told reporters, "Those who oppose the freedom to choose believe that this is their time." That's a belief that Bush has done everything he can to encourage.</P>
<P>There's no telling how many women internationally must seek unsafe abortions each month because of Bush's re-imposition of the "global gag rule," which prevents international family planning organizations from mentioning abortion if they accept U.S. government funding.</P>
<P>Bush's telephone speech to the anti-choice rally came just two days after "National Sanctity of Human Life Day." Bush's statement marking the day declared the "essential moral duties" of society, including "defending the elderly, strengthening the weak, protecting the defenseless, feeding the hungry, and caring for children--born and unborn."</P>
<P>What hypocrisy! Never mind that the Bush administration's tax breaks for the rich have led to a rise in homelessness, especially among families, and that more elderly people are going without medical care.</P>
<P>If Bush was really interested in "caring for children," he might start in Afghanistan. Dozens of children have died in just the past few weeks from the terrible remains of U.S. carpet bombing--"bomblets" from cluster bombs that rip anyone who touches them to pieces. Hundreds of thousands of Afghan women and children are suffering in refugee camps with little or no food because the administration has dragged its heels in sending economic aid.</P>
<P>But that's not enough suffering for Bush, apparently. So he recently decided to hold up $34 million in U.S. aid to the United Nations Population Fund.</P>
<P>Why? Because a few right-wing cranks in Congress insist that the program funds abortions.</P>
<P>The claims are a fabrication. But if the anti-choice bigots are upset, then Bush will listen--no matter what the cost in human suffering.</P>

