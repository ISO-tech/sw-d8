<font face="Times New Roman, Times, serif" size="3">Accusations against Finkelstein typically come from supporters of Israel who insist upon equating opposition to Zionism with anti-Semitism. In particular, Finkelstein has been dogged by Harvard Law School professor Alan Dershowitz, who harassed professors and staff at DePaul, reportedly sending a 13-page fax to every member of the DePaul Law School; wrote members of the Political Science department, including those on the tenure committee; and requested to be present at Finkelstein's private tenure hearing.<p>
Dershowitz is just one glaring example of the pressure that outside forces have brought to bear on the university to deny Finkelstein tenure. The process so far has gone in his favor, but as a decision nears, hurdles have been thrown in the way.<p>
The Political Science Department's 9-3 decision supporting Finkelstein's tenure was followed by a unanimous 5-0 vote in his favor from the College Personnel Committee of the College of Liberal Arts and Sciences.<p>
In the past, the dean has always confirmed a unanimous decision. But this time, Dean Chuck Suchar filed a report not supporting Finkelstein's application for tenure. If tenure isn't granted, a professor is fired after a one-year grace period. So in reality, Suchar is recommending that the university fire Norman Finkelstein.<p>
Suchar claims that the "tone and substance of his scholarship...is inconsistent with DePaul's Vincentian values...as well as our commitment to diversity," a statement apparently justified by Finkelstein's personal criticism of those who disagree with him. By the same token, Finkelstein is regularly called the worst names by "reputable" scholars in his field.<p>
There are two more key decisions to be made--by a university board and the president, Rev. Dennis Holtschneider. Students and faculty at DePaul University plan on making it clear that they are watching--and ready to take action if university officials don't do the right thing.<p>
Students recently formed a group to defend Finkelstein, and have created a petition to send to President Holtschneider. A demonstration is planned to show support for Finkelstein as he departs for the University Board meeting on April 13--meet outside the Political Science Department, 990 W. Fullerton, 2nd floor.<p>

