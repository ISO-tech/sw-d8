<font face="Times New Roman, Times, serif" size="5"><b>DePaul prof under fire for criticizing Israel</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Kathryn Weber</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 13, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">SUPPORTERS OF academic freedom are organizing behind Norman Finkelstein, a DePaul University political science professor, author and outspoken supporter of Palestinian liberation.<p>
Finkelstein--the author of five books, many of them internationally known--has traveled the world speaking at events ranging from scholarly lectures to speeches for Palestinian justice groups. But back on DePaul's campus in Chicago, he's facing a battle over gaining tenure.<p>
Finkelstein's students regularly give him rave reviews as a teacher. The dean of DePaul's College of Liberal Arts and Sciences even referred to his classroom as "a transformative experience."<p>
But Finkelstein has suffered throughout his academic career for his political convictions. As a result of his support of the Palestinian people, he has been derided as a Holocaust denier and "self-hating Jew."<p>
Finkelstein's mother and father were Holocaust survivors; he certainly has never denied the Holocaust. He says he simply does not believe that the suffering of one group of people gives them the right to make another group suffer.<p>

