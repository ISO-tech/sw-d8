<font face="Times New Roman, Times, serif" size="5"><b>The torturers cry foul</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 13, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">TO HEAR Tony Blair and George Bush describe the treatment of 15 British sailors detained by the Iranian government, you'd think they had been...well, tortured in a U.S. military prison.<p>
Maybe "waterboarded"--the simulated drowning used to extract information at Abu Ghraib, Guant&aacute;namo Bay and secret CIA facilities around the world. Or had electrodes strapped to their genitals--another torture technique seen in the photos of Iraqi prisoners at Abu Ghraib.<p>
Perhaps forced to undergo sensory deprivation or beaten just short of "organ failure"--methods approved in the notorious "torture memo" written at the request of Alberto Gonzales when he was White House counsel.<p>
In reality, the British sailors were unharmed during their detention--leaving politicians and talking heads on both sides of the Atlantic struggling to explain why the sailors appeared on television to apologize for apparently straying into Iranian waters while inspecting ships.<p>
The British patrols are one part of a U.S.-led attempt to isolate and pressure Iran over its nuclear program--a campaign that includes economic sanctions, diplomatic isolation, the arrest of Iranian officials in Iraq, and U.S. sponsorship of armed resistance groups carrying out attacks in Iran.<p>
The capture of the British sailors was a pushback by Iran--as was the subsequent announcement by Iranian President Mahmoud Ahmadinejad that Iran had achieved the capacity to mass-produce nuclear centrifuges, a significant development in its nuclear program.<p>
U.S. and British pundits fretted that the sailors had been too grateful toward their captors. Yet they also insisted that videos of the sailors relaxing, eating, smoking cigarettes and chuckling with Ahmadinejad before their departure were coerced.<p>
On the first day of the sailors' return, "at no point did they retract their repeated words of gratitude to their captors, or suggest that the Iranians had treated them unjustly," according to the <i>Globe and Mail</i>, Canada's leading newspaper.<p>
Within two days, however, the British government had paraded the sailors on television with canned statements--this time with stories of plastic handcuffs, blindfolds and perhaps a mock execution.<p>
For anyone who saw the Iranian footage of the sailors on television, their smiles and generally relaxed demeanor suggested at the minimum that they had not been grossly abused.<p>
Bush and Blair's complaints about Iran's treatment of the sailors are revealing. Either they think the American and British publics are too stupid to recognize the difference between U.S. abuse of Iraqi prisoners and Iran's handling of the British detainees.<p>
Or they believe what the sailors were subjected to really is mistreatment, but the torture that U.S. and British forces have inflicted on Iraqis isn't mistreatment--a disgusting double standard that speaks volumes about their racist disregard for people enduring the U.S. occupation.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">BUT THE uproar over the treatment of the British sailors is only one example of the hypocrisy that pervades the U.S. and British conflict with Iran.<p>
This latest confrontation began with the stepped-up targeting of Iranian officials by the U.S. military in recent months. In January, just after George Bush announced his plan for a "surge" of U.S. troops in Iraq, U.S. forces abducted five Iranian officials from the Iraqi city of Erbil.<p>
Also, Jalal Sharafi, a cabinet member under former President Mohammad Khatami, went missing under mysterious circumstances in Turkey in February. Sharafi reappeared last week--saying that he had been tortured by CIA agents and showing bruises on his body to the Iranian press--even as Britain and the U.S. denied that his release had anything to do with the return of the sailors.<p>
Overshadowing all this is Washington's effort to stoke an international crisis over Iran's nuclear program. Enriching uranium to provide material for civilian power plants is allowed under the Nuclear Non-Proliferation Treaty, but U.S. claims that Iran is developing nuclear weapons is meant to play the same role in Washington as weapons of destruction did in the campaign against Iraq--a pretext for aggression aimed at shoring up U.S. dominance in the Persian Gulf.<p>
With its occupation of Iraq in crisis, the Bush administration is looking to "double down" by confronting Iran--making an even bigger bet to make up for earlier losses and gain control of more Middle Eastern oil.<p>
The U.S. and its junior partner Britain are the real source of violence in the Middle East, and no manufactured hype about the supposed mistreatment of British sailors will change that.<p>

