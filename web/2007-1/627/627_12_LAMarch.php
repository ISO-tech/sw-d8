<font face="Times New Roman, Times, serif" size="5"><b>LA immigrant march sends a message</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Robert D. Skeels and Shaun Harkin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 13, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">THE STREETS of downtown Los Angeles became a sea of red shirts stretching for blocks on April 7 when at least 20,000 people turned out for one of the largest immigrant rights demonstrations since the mega-marches last spring.<p>
Organizers, who expected no more than the 800 or so people who gathered for a similar demonstration two weeks ago, were surprised by an overflow crowd that stretched the length of the two-mile route down Broadway Avenue to LA City Hall.<p>
The turnout was driven by opposition to the Bush administration's new immigration reform proposal, recently released in draft form.<p>
The White House's plan contains many of the enforcement provisions in the right-wing Sensenbrenner bill that stalled in Congress last year because of the mass marches--such as a fourfold increase in border fencing and a more than 50 percent increase in Border Patrol agents.<p>
It would also establish a new "Z" visa that would allow undocumented immigrant workers to apply for three-year work permits--renewable indefinitely, but at a cost of $3,500 each time.<p>
To get a permit and become legal permanent residents, undocumented immigrants would have to return to their home country, apply at a U.S. embassy or consulate to re-enter, and pay a $10,000 fine. The cost for work permits and the green card application would total more than $20,000--well out of reach for the vast majority of low-wage immigrant workers.<p>
The Bush proposal would break up of immigrant families, too, according to the <i>Washington Post: </i>"In a new twist, more green cards would be made available to skilled workers by limiting visas for parents, children and siblings of U.S. citizens. Temporary workers could not bring their families into the country."<p>
As LA protester Armando Garcia told a reporter for the Associated Press, "Charging that much, Bush is going to be even more expensive than the coyotes." Francisco Gomez agreed. "For my wife and I, it would cost about $30,000," he said. "Multiply that by all the illegal immigrants here...It's obvious that Bush just wants to fund his Iraq war with our money."<p>
Maria L&oacute;pez, an undocumented immigrant who works as a seamstress and sends $200 a month home to family members in Mexico, said, "We have no way to come up with that much money, and Bush knows that. He is doing this on purpose so we don't ever become legal residents."<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">DURING THE protest, thousands of people carried bright red "Amnistia" placards, as well as banners and signs saying "Alto A Las Redadas" (Stop the Raids), and "Alto Los Deportaciones" (Stop the Deportations). The lively and determined crowd chanted, "El pueblo unido, jamas sera vencida" (The people united, will never be defeated), and "?Que queremos? !Amnistia!" (What do we want? Amnesty!).<p>
Many protesters heard about the event on local Spanish media. "I head about this on El Piolin radio," said one marcher. "Many people say the movement is getting smaller. I think this is going to send a message."<p>
Demonstrators were also aware of the proposal put forward by Reps. Luis Guti&eacute;rrez and Jeff Flake, named the STRIVE Act. Though seen as less draconian, the STRIVE Act would also create a guest-worker system not so dissimilar to the one Bush is pressing for. "We didn't march last May for a bracero program," said a protester named Raina.<p>
The turnout in LA is certain to build momentum for a national day of action on May 1. Events are already planned in at least a dozen cities, from LA to Boston, and Chicago to North Carolina.<p>
Significantly, International Longshore and Warehouse Union locals at six West Coast ports have resolved to participate in the day of action by holding "stop work meetings" in support of workers and immigrant rights.<p>
The size and anger of the Los Angeles march and rally is a clear indication that the immigrant rights movement has not "disappeared." May 1 will be an opportunity to organize the anger against Bush's proposal--and build the fight for demands that go beyond what the Democrats and Republicans in Congress are willing to put forward.<p>

