<font face="Times New Roman, Times, serif" size="5"><b>Is Gonzales' head ready to roll?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 13, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE SCANDAL over the Bush administration's political firing of U.S. attorneys is coming closer and closer to taking down the country's top law enforcement official.<p>
Attorney General Alberto Gonzales' aides are going fast--former chief of staff Kyle Sampson quit last month; so did Michael Battle, who carried out the firings; and last week, Gonzales' top counselor Monica Goodling resigned after refusing to testify to Congress.<p>
This week will be filled with speculation about what Gonzales will say if and when he appears, as promised, before Congress on April 17. But as the day approached, even right-wing Republicans like ex-House Speaker Newt Gingrich were calling for Gonzales to quit.<p>
The administration is in trouble for firing eight U.S. attorneys late last year. U.S. attorneys have been fired before, but rarely a group of them in the middle of their terms, and not for such baldly political reasons.<p>
Several apparently got the boot for failing to seek death sentences with the enthusiasm expected of them by Gonzales and his predecessor John Ashcroft.<p>
Others fell afoul of Republicans who wanted the Justice Department to harass Democrats. For example, fired prosecutor David Iglesias in New Mexico says he was pressured by Sen. Pete Domenici to investigate alleged claims of vote fraud by Democrats. When Domenici didn't get his way, he complained to George Bush--and Iglesias was pushed out shortly thereafter.<p>
It's not like the fired prosecutors were raving radicals. Iglesias was seen as a rising Latino star in the Republican Party. He criticized the Bush administration's stand on immigration <i>from the right</i>--demanding, in an op-ed article for the uber-conservative <i>Washington Times</i>, tougher laws to stop border-crossers and "alien smugglers."<p>
It's a sign of the administration's arrogance that it could still decide that the likes of Iglesias were insufficiently loyal.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">IN DEFENDING itself, the Bush team is taking a page from Richard Nixon--whose advice to his Watergate co-conspirators, preserved on a secret White House tape, was: "You can always say you can't recall."<p>
Kyle Sampson used the phrase "I don't remember" 124 times in testimony before the Senate Judiciary Committee in late March. But Sampson remembered enough to contradict his ex-boss' claim to have not been involved in any discussions about the firings.<p>
Of course, Gonzales and <i>his </i>boss are guilty of far greater crimes. Gonzales is the chief author of the administration's "redefinition" of torture to allow interrogation tactics forbidden under any normal understanding of international law. Every senator knew about this scandalous behavior before Gonzales' nomination was voted on in 2005--but he still won confirmation by a comfortable margin.<p>
Like any political scandal, though, Attorneygate is about more than the specifics of this case. It gives a glimpse of the inner workings of a corrupt system--the reality behind the rhetoric about democracy and serving the people.<p>
The Bush administration's fixation with installing its allies in office confirms a lesson from the Hurricane Katrina nightmare--that in Washington, it isn't what you know, but who you know and how loyal you'll be in carrying out a partisan political agenda.<p>
Then there's the shady means the Bush administration used--a provision quietly slipped into the reauthorization of the USA PATRIOT Act allowing the White House to appoint U.S. attorneys without Senate approval.<p>
Bush would never be under this kind of pressure a few years ago. The scale of the scandal shows the growing discontent with the Bush administration and its ineptitude on any number of issues--including Katrina, but especially the ongoing crisis of the occupation of Iraq.<p>
The Bush administration's basic competence to run the world's most powerful government is in question--not just among ordinary people, but within the ruling circles of U.S. society. But we think Bush and his clique of hypocrites and political fixers should be held accountable for all of their crimes.<p>

