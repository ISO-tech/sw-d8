<font face="Times New Roman, Times, serif" size="5"><b>CEOs cut jobs and line their pockets</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 13, 2007 | Pages 1 and 4</font><p>

<font face="Times New Roman, Times, serif" size="3">HOW WOULD you like to be paid $39 million for four months of work? That's the situation for Alan Mulally, the president and chief executive of Ford Motor Co.<p>
According to recent news reports, when Mulally took over as CEO of Ford on September 1 of last year, he immediately started raking in the big bucks. For just the four months he worked last year--September 1 until December 31, a total of 122 days in all--Mulally received total compensation valued at $39.1 million. That's $320,491 a day--including weekends.<p>
In addition to his $666,667 in straight salary--a prorated amount based on his $2 million annual "earnings"--Mulally also got a $7.5 million hiring bonus and $11 million to offset performance and stock option awards he forfeited when he left his previous company. He received other compensation totaling $334,433, including $172,974 for use of corporate aircraft and $55,469 for "relocation costs" and "temporary housing."<p>
And to top it off, Ford gave Mulally stock and option awards worth an estimated $19.6 million when they were granted to him on his first day on the job, according to the Associated Press.<p>
No wonder Mulally praised his fellow Ford executives when he opened the recent New York International Auto Show. "Look at the smiles on their faces," he said, pointing at them.<p>
Ford workers have less reason to smile these days. Last year, the country's second-largest automaker lost $12.7 billion, the largest loss in its history. Management, led now by Mulally, has undertaken a massive restructuring program aimed at cutting costs through cutting jobs.<p>
The company announced last year that it would seek to close 16 plants and slash at least 30,000 jobs by 2012. Additionally, Ford and other automakers have squeezed workers' health care and pension benefits.<p>
In the past year and a half, the United Auto Workers has agreed to concessions in retiree health care at Ford worth $850 million, as well as lower pay scales for new workers and buyout programs that have prompted tens of thousands of workers to retire early or take a lump sum in cash and give up their jobs and benefits.<p>
If, on the other hand, Mulally ever decides to leave, he'll have a golden parachute to break his fall. According to the <i>Detroit Free Press</i>, his severance package will be worth $27.5 million if he's terminated "not for cause" or for "good reason," such as a change in control at the company.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">OF COURSE, Alan Mulally isn't alone. Other CEOs are feeling equally flush these days.<p>
Verizon chief executive Ivan Seidenberg earned more than $109 million in the past five years--despite the company losing money while he was in charge. Former Home Deport chief Robert Nardelli was given approximately $210 million to clean out his desk--including $20 million in cash severance and $77 million in deferred stocks--on top of the $240 million in compensation he received during the six years that he was in charge of the company.<p>
Meanwhile, the Bush administration announced with much fanfare last week that the unemployment rate had dropped to 4.4 percent in March, matching a five-year low. But for factory workers--including in auto, furniture, clothing and textiles--the number of jobs declined for the ninth straight month. Residential construction jobs, a casualty of the housing slump, also fell.<p>
Nevertheless, according to economists, while unemployment may have fallen somewhat last month, wage growth isn't accelerating.<p>
According to the Center on Budget and Policy Priorities (CBPP), data released by the federal government last month showed that the share of national income going to wages and salaries in 2006 was at its lowest level ever recorded, with data going back to 1929. By contrast, the share of national income captured by corporate profits was at its highest level on record.<p>
"As a consequence, wages and salaries have captured an exceptionally small share of the total growth in national income that has occurred in the current period," reported the CBPP. "Only 34 percent of the overall increase in national income since the end of 2001 has gone to increases in workers' pay, a smaller fraction than in any other expansion since World War II. For the first time on record, corporate profits have captured a larger share of the income growth in a recovery--46 percent of it--than wages and salaries have."<p>

