<font face="Times New Roman, Times, serif" size="5"><b>Victory for Cal State faculty</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 13, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>LANCE NEWMAN reports on why CSU administrators caved to the faculty union's demands.</b><p>

IN A major reversal after 23 months of stonewalling, the administration of the California State University (CSU) system has settled a contract that gives the faculty union everything they demanded and more.<p>
For the past two years, the administration insisted it could only afford a 15 percent across-the-board raise over four years. It tried to institute an arbitrary merit pay scheme that would have rewarded cronies of the higher-ups.<p>
And because faculty loudly opposed executive perks and nonstop student fee increases, the administration tried to tie salary increases to a gag clause that would have taken away the union's right to publicly criticize the CSU budget.<p>
The administration attacked faculty in the media, painting them as fat-cat professors who make $86,000 a year for working only nine months. In fact, that's the average salary for full professors who have at least 12 years of seniority--a tiny percentage of the overall workforce. More than half of the faculty in the 23-campus CSU system are lecturers and part-timers who earn less than $40,000 a year, usually with no benefits.<p>
The real fat cats are in the administration--where Chancellor Charles Reed makes $377,000 a year, more than double the state governor's salary, and where retiring executives are routinely rewarded with golden parachute payouts worth hundreds of thousands of dollars.<p>
Despite the administration's intransigence, the California Faculty Association (CFA) stood fast--and won almost an 21 percent across-the-board raise for faculty in the tentative agreement that headed off a possible strike.<p>
Additional "service-step" raises will boost some professors' salaries by an additional 11 percent. The contract also includes increased job security for part-timers and untenured lecturers, as well as improved maternity/paternity leave benefits and streamlined grievance procedures.<p>
The union ran a highly disciplined campaign that relied on the power of an active rank and file. Teams of faculty members hounded Charles Reed, protesting outside his public speaking events around the country. In November of last year, a crowd of 1,500 faculty and student activists staged a sit-in at a meeting of the CSU Board of Trustees.<p>
When the bargaining process reached an impasse, the CFA took a strike-authorization vote in which 81 percent of union members cast ballots. Ninety-four percent voted in favor of a strike.<p>
This demonstration of rank-and-file solidarity brought the administration back to the bargaining table with its tail between its legs. This was immediately followed by a neutral mediator's report that supported the faculty's bargaining position down the line.<p>
Faced with a faculty strike that would shut down the university system, and reeling from nonstop media revelations of bureaucratic corruption, the administration accepted the union's contract proposal with almost no changes.<p>
This is a huge victory not only for CSU faculty, but for the labor movement as a whole. It shows the power of an organized rank and file that is willing to use its most powerful weapon--the strike.<p>
At a press conference announcing the agreement, CFA President John Travis promised that the next item on the union's agenda would be rolling back student fee hikes. The faculty has shown who is in charge at CSU, and now is the time to use that power to make the university deliver on its promise to provide affordable higher education to the children of California's working-class families.<p>

