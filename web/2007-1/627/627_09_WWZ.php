<font face="Times New Roman, Times, serif" size="5"><b>Where were you in the zombie war?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Sarah Grey</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 13, 2007 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Max Brooks, <b><i><a href="http://www.amazon.com/gp/redirect.html?ie=UTF8&location=http%3A%2F%2Fwww.amazon.com%2FWorld-War-Z-History-Zombie%2Fdp%2F0307346609%3Fie%3DUTF8%26s%3Dbooks%26qid%3D1176256914%26sr%3D8-1&tag=socialistwork-20&linkCode=ur2&camp=1789&creative=9325" target="_blank">World War Z: An Oral History of the Zombie War</a>. </b></i>Crown, 2006, 352 pages $24.95.</font><p>

<font face="Times New Roman, Times, serif" size="3">IN 2005, the world watched in horror, and George W. Bush twiddled his thumbs, as Hurricane Katrina bore down on a defenseless Louisiana. The Bush administration's obsession with "homeland security" did not extend to taking the obvious measures necessary to save the people of New Orleans from disaster.<p>
And as climate change and the threat of pandemic disease grow each year, a recent study showed that few U.S. cities are prepared to care for their citizens in the case of a major disaster.<p>
What does this have to do with zombies? Everything, according to <i>World War Z: An Oral History of the Zombie War. </i>Written in the interview style of Studs Terkel's classic <i>Working,</i> <i>WWZ</i> is a fictional account of what happens when politicians put profit and ideology before human need.<p>
In <i>WWZ's </i>future, a mysterious virus appears in rural China around 2008, just as the Bush administration is winding down and the American people have demanded an end to the war in Iraq. The virus, initially known as "African rabies," kills its victims within a few days and then re-animates their dead bodies, which then become zombies intent on eating human flesh.<p>
Anyone bitten by a zombie is doomed to become a zombie. Initially, studies are issued showing that isolated outbreaks of the virus have the potential to become a global pandemic, but the reports are shelved in an election year, and those who protest the government's neglect of the issue are labeled "NPR liberals" and ignored.<p>
By the time world governments begin to acknowledge the zombie threat and take action, it's too late, and the zombie uprising is unstoppable. It results in a global human-zombie war that lasts 10 years, devastates the earth, rearranges the world map and (perhaps) renders capitalism forever irrelevant.<p>
Brooks' narrator travels the globe 10 years after the end of the war, interviewing survivors, soldiers, profiteers and politicians about their role in "World War Z." Brooks' vision of the future is cynical and bitter.<p>
Drug companies profit from the crisis by marketing useless drugs as miracle cures, causing the deaths of thousands. A nuclear crisis erupts in South Asia as Indian refugees stream through Pakistan and into Iran, causing a nuclear exchange between Iran and Pakistan.<p>
Israel, out of desperation, grants Palestinians the right of return and uses its apartheid wall to quarantine itself from the world; it remains safe from the zombie menace but is rocked by a civil war when right-wing Zionists revolt.<p>
The U.S. military abandons its citizens on the East Coast, moves the federal government to Hawaii, and uses the Rocky Mountains as its line of defense. And Cuba, relatively safe as an island, finds itself overrun with millions of refugees from the U.S., houses them in refugee camps and develops "guest-worker" programs to allow Americans to "do the jobs Cubans don't want to do."<p>
<i>World War Z </i>is an impressive achievement of speculative fiction: It is both a trenchant left-wing political critique and a well-written page-turner that will satisfy the most demanding horror and sci-fi fans.<p>

