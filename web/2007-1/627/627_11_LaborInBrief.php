<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">April 13, 2007 | Page 10</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Hayward, Calif., teachers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By John Green</b></font><p>

<font face="Times New Roman, Times, serif" size="3">HAYWARD, Calif.--Some 1,300 public school teachers struck for two days April 4 and 5, shutting down 38 schools in a district of 20,000 students.<p>
Teachers walked out over unresolved contract negotiations. They are demanding a decent salary hike--especially in light of top administrators getting a 16.8 percent raise this year.<p>
The school district offered teachers a 7 percent raise beginning in July, instead of the customary raise retroactive to the start of the school year. Additionally, the district offered a one-time 3 percent bonus and held out promise of an additional 1.7 percent hike next year if enough veteran teachers agree to retire.<p>
Teachers described the offer as insulting. They pay for all their health care costs out of their salary, which reduces some teachers' compensation by $10,000 a year.<p>
Many parents and students joined the picket line. Interviews on local television highlighted the depth of solidarity. "Community support has been wonderful," Kathleen Crummey, president of the Hayward Education Association, said in an interview. "We've held large rallies both days. Dolores Huerta addressed us today. To our community, she's a rock star."<p>
For weeks, the school district urged students to attend, bragging that they had 1,000 scabs at their disposal. But the district admitted that just 21 percent of students went to school on the first day of the walkout--and the media speculated that most of the older students left at lunch anyway.<p>
The students told tales of chaos as administrators--not 1,000 scabs--kept them together in the gym all day.<p>
The strike ended without a resolution on the eve of the weeklong spring break, during which time both sides were expected to negotiate. But Superintendent Dale Vigil left for vacation, leaving his lieutenants in charge. "As a leader, he should lead this district into settlement," Crummey said. "Instead, he's led us into crisis and chaos."<p>

