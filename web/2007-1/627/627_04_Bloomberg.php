<font face="Times New Roman, Times, serif" size="5"><b>Bloomberg's insult to the poor</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 13, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">RECENTLY, NEW York City Mayor Michael Bloomberg announced a disgusting new social engineering experiment that would monetarily "reward" poor families for improved school attendance, procuring medical insurance, and finding, keeping and holding a job.<p>
To some, the program appears to be philanthropic manna from heaven in a city that has seen working-class living standards plummet in recent years, where five city hospitals are on the chopping block and gentrification is running amok.<p>
The program bases itself on the premise that providing financial incentives--like $25 for two months of regular primary school attendance, or $50 for high school students--would compel the poor to make "better" decisions. As if the reason that the one in five New Yorkers living in poverty do so is because they lack the incentive to make the decisions that would better their economic situation!<p>
The <i>New York Times</i> describes the program as operating "like a philanthropic foundation and in part like a venture capital company." As the <i>Times</i> puts it, it will utilize the "the private sector to tackle problems that have historically vexed governments."<p>
This new city agency, funded with a mixture of public and private monies, would be called the Center for Economic Opportunity. In name seemingly benign, the program would seek to place the onus for individual success on a working class ravaged by the system-wide and collective problems of poverty, inequality and racism.<p>
Bloomberg, at the press conference announcing the new program, was quoted by the <i>Times</i> as saying "conventional approaches, as we know, have kept us in this vicious cycle of too many people not being able to work themselves out of poverty even though they're doing everything that we've asked them to do."<p>
Attempting to shift the government's responsibility of solving the problem of poverty with a free-market-driven solution that places the fault on individuals is neither unconventional nor novel: It's called neoliberalism. The program's attempts to encourage "good behavior" and self sufficiency within the 2,500 families selected for the program are a slap in the face to the millions of New Yorkers living in poverty.<p>
Comparing the $124 billion war budget just approved to continue the war in Iraq, to the $25 to $300 that families could be eligible to receive every two months for "good behavior" is a glaring example of what the priorities of those in power are.<br>
<b>Jared Rodriguez</b>, New York City<p>

