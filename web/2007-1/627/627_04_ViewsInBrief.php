<p><a name="Words"></a><font face="Times New Roman, Times, serif" size="4"><b>Choosing our words carefully</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I'M A new member of the International Socialist Organization (ISO) and don't have much experience in the struggle like others, but a recent article by Sharon Smith came as a shock to me ("The Democrats' plan to save the Bush agenda," March 23). I really enjoy reading her columns, but when she used the word "cojones," it was a surprise.<p>
I understand that the ISO stands for fighting oppression of any kind, and I think that sexism is a huge, long struggle that we have to fight. <i>Socialist Worker</i> is the way we bring our politics to the streets and bring people to our organization. When we put the word "cojones" in <i>SW,</i> it is hard to actually say that we are fighting that struggle.<p>
We want to fight in any way possible, and that means starting with our paper. I don't think "cojones" should be used, because a woman should not be compared to a man's genitals to say that she is as strong as a man or has guts--even if she is Hillary Clinton, a Democrat, and she stands for all the wrong things. I think that when the word "cojones" is used, you are still putting women in the shadow of capitalism.<p>
I respect Smith's work, but I wanted to bring this up because we need to be clear on what wording we use. I think that sexism should be fought in the workplace, at home, among friends and anywhere we can.<br>
<b>Jose L.</b>, San Francisco<p>

