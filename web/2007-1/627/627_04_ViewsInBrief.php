<p><a name="Forced"></a><font face="Times New Roman, Times, serif" size="4"><b>Forced to choose between work and kids</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ON MARCH 28, this story appeared in the <i>Cincinnati Enquirer</i>: Beverly Fears, 24 years old, left her two young children, a 5-year-old and a 20-month-old, at home alone at 2:12 a.m. and went to work. Someone, it's not clear who, called the police, who went to her job two-and-a-half hours later, and arrested her.<p>
She was to appear in court on March 29 to answer charges of child endangering. This charge carries a maximum prison sentence of 10 years.<p>
Who is really at fault here? Fears did not go out to a party with her friends; she went to her job. The news story did not go into her current financial situation or why she did not have someone to watch the children, but this situation has played out over and over again, where young single moms, with very young children have been thrown into our already over-crowded prison system as a result of so-called "welfare reform" enacted in the mid-1990s, which forced welfare mothers to go to work, without access to child care.<p>
Whether Fears was forced to go to work because she was caught up in the welfare system or because she wanted to avoid being caught up in the welfare system, the fact remains that she did not have access to child care, and she had to go to work to provide food and shelter for her family.<p>
Who's at fault here? Perhaps it's time to take another look at "welfare reform." Perhaps it's time to put whatever remains of our current welfare system on trial, rather than our impoverished citizens.<br>
<b>Vera Zlatkin</b>, Cincinnati<p>
