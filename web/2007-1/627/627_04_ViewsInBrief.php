<a name="Wal"></a><font face="Times New Roman, Times, serif" size="4"><b>Wal-Mart's plan for a new image</b></font><p>

<font face="Times New Roman, Times, serif" size="3">RECENTLY, I was listening to a BBC new report that introduced a new campaign being launched by Wal-Mart's corporate heads. This "Employee Self-help Initiative" is designed to encourage Wal-Mart's 1.4 million employees to take the initiative in kick-starting new programs that will improve their lives. Programs for self-help include developing recycling routines, eating healthier foods and even working to quit smoking.<p>
Wow, it's brilliant: Instead of doing things that will actually improve the lives of workers, blame their personal activities and habits as individuals for their alienation. The wellbeing of Wal-Mart workers is supposedly about the personal willpower of the employees.<p>
This sham of a publicity stunt borders on Stalinist propaganda. In a time when employers are on the offensive, corporations need to take attention away from the fact that it is their unquenchable thirst for profit that is responsible for the deplorable state of the working class.<p>
Workers need health care, living wages and unions that will fight to win such things. If Wal-Mart wants better employees, the corporate heads should put caps on their own salaries, instead of their workers' wages, and give their employees the dignity and respect they deserve.<br>
<b>Alessandro Tinonga</b>, Santa Cruz, Calif.<p>
