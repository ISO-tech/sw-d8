<p><a name="Mumia"></a><font face="Times New Roman, Times, serif" size="4"><b>Justice for Mumia</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Brown and Dylan Stillwood</b></font><p>

<font face="Times New Roman, Times, serif" size="3">PHILADELPHIA--Lawyers representing Mumia Abu-Jamal appeared before the Third U.S. Circuit Court of Appeals May 17 to present arguments in Mumia's last chance at a new trial.<p>
Mumia, a former member of the Black Panther Party and radical journalist well known for exposing racist police brutality, was framed and sentenced to death for the 1981 murder of Philadelphia police officer Daniel Faulkner.<p>
Around 200 Mumia supporters packed the courtroom, with a handful of retired police on hand representing the other side. Another 700 protesters gathered outside the court to rally for Mumia.<p>
Three issues at stake in the latest appeal cut to the heart of the racism and sloppy nature of the original trial. First, the prosecutor used 10 of his 15 peremptory strikes to eliminate Black jurors from the jury pool, leaving only two Blacks on the final jury--in a city that is nearly half African American.<p>
Secondly, the prosecutor told the jury that their verdict "may not be final" because "there would be appeal after appeal"--an unconstitutional way to make them feel less responsible for the decision to give Mumia the death penalty.<p>
If the court agrees with either of these arguments, then Mumia will likely get a new trial.<p>
Additionally, Mumia's initial appeal--known as a "PCRA hearing"--was overseen by Albert Sabo, the same racist and pro-death penalty judge who presided at the original trial. If the court agrees that Sabo was biased, then Mumia will be eligible for a new PCRA hearing.<p>
The stakes are high. Though Mumia's death sentence was overturned in 2001, the state is challenging this decision. If the court rejects Mumia's appeals but agrees with the state, he could have a new execution date within months.<p>
The public spotlight on this case will be important in keeping the pressure on the court. At the rally, Fred Hampton Jr., son of the slain Black Panther leader, drew parallels between his father's murder by Chicago police and Mumia's case. He also stressed the importance of building the fight for Mumia outside of the courts. "The lawyers can do their job in the courtroom," he said, "but we have to do our job in the streets."<p>
As Barbara Easley-Cox, a former Black Panther and friend of Abu-Jamal's, told the crowd, "What is at stake here," she added, "is the example of what the state can do to people unless we stand up and fight back."<p>
