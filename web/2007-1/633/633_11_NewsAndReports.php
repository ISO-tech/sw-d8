<p><a name="War"></a><font face="Times New Roman, Times, serif" size="4"><b>Support the war resisters</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Somerset Stevens</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--More than 100 people came out May 14 to hear war resisters Agust&iacute;n Aguayo, Pablo Paredes and Camilo Mej&iacute;a--all members of Iraq Veterans Against the War (IVAW)--speak about their experiences in Iraq and Afghanistan.<p>
Newly released from the brig for his refusal to fight, Aguayo said that he had joined the Army believing that he would do good. But he recalled an officer telling his unit, "If you guys are having questions about the Geneva Convention, stop that...If you're going to fire your weapon you have to finish the job."<p>
Based on these and other experiences, Aguayo came to the conclusion that he couldn't take a life, and refused to load his gun.<p>
All three men made connections between the war and racism. Mej&iacute;a described a "long and proud history of resistance in the U.S. military" and urged the audience to take action to support military resisters.<p>
He described his first assignment in a prisoner of war camp, being trained to interrogate, to deprive prisoners of sleep, hold guns to their heads and simulate explosions. "As I was telling my stories for the first time it became very difficult not to question myself...It became impossible for me to push aside my conscience," he said.<p>
Paredes recalled getting letters in the brig from Mej&iacute;a and civilian activists, saying that they "helped me find freedom behind bars."<p>
All three men described the growing anger within the armed services. As the panel showed, the need antiwar movement that stands in solidarity with war resisters, opposes U.S. interventions and has the power to embolden and defend soldiers who refuse to fight is more important than ever.<p>
As Paredes told the crowd, "I did the right thing. I can look my children in the eye, I can speak with pride. I have no regrets."<p>

