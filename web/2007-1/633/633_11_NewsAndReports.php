<a name="LAPD"></a><font face="Times New Roman, Times, serif" size="4"><b>Protest the LAPD's May Day attack</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs</b></font><p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES--On May 17, more than 10,000 people converged on MacArthur Park, the site of the LAPD's May Day attack on immigrant rights protesters, to renew demands for justice and civil rights for all immigrants and to declare that "our voices will not be silenced!"<p>
For many marchers, this was an angry protest against the LAPD. Dozens of hand-made <i>Socialist Worker</i> placards protesting police brutality and calling for Bratton to be fired were enthusiastically taken up by some marchers.<p>
Mar&iacute;a, a speaker on behalf of the Youth Justice Coalition (YJC), opened her speech with the chant, "La migra, la polic&iacute;a, la misma porquerr&iacute;a!" (Border patrol, police, they're all the same pigs!) YJC played an important role in challenging the willingness of some organizers of the May 17 rally to join the police in blaming "young agitators" and "anarchists" for the LAPD attack.<p>
However, the message from some was more equivocal. March organizers and invited both Los Angeles Mayor Antonio Villaraigosa and LAPD Chief William Bratton to participate.<p>
Standing next to his beleaguered police chief, whose appointment to a second five-year term is now in jeopardy following the police riot, Villaraigosa promised the crowd that "justice will be realized." One local labor leader declared that "this is a moment of healing."<p>
But many were not so eager for reconciliation with the police. When marchers reached the park, they had the opportunity to hear a number of speakers who angrily denounced the cops.<p>
Marquise, a representative of the Community Coalition, a largely African American organization, told the crowd, "We were here with you when the police tried to run us out, and we're back here with you today. We're not going to let the LAPD turn us around!"<p>
This march shows the resiliency of the immigrant rights movement in Los Angeles, even after being brutally attacked by the police. It also shows the political differences between a leadership focused on conciliation and compromise and grassroots activists eager for a more determined and militant fight for legalization and equal rights for all.<p>
