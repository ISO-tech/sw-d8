<font face="Times New Roman, Times, serif" size="5"><b>Fighting for his life from Texas death row</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 25, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>BRYAN McCANN reports on the struggle to save Kenneth Foster Jr.</b><p>

TEXAS DEATH row prisoner Kenneth Foster Jr. and his supporters received the latest in nearly a decade of bad news in May: After the U.S. Supreme Court refused to hear his latest appeal, the state of Texas set his execution date for August 30.<p>
A founding member of the Death Row Inner-Communalist Vanguard Engagement, or DRIVE, Movement, Kenneth is a respected leader leader of the abolitionist movement in the state that puts more people to death than all others combined.<p>
Now, if Texas gets its way, Kenneth will be put to death in just over three months--for the crime of driving a car.<p>
Kenneth was convicted and sentenced under Texas's Law of Parties, which allows prosecutors to seek equal sentences for anyone present at the time of a crime, even if they didn't commit it. All the state has to do is prove the defendant "should have anticipated" that a crime was going to be committed in order to hold them legally responsible for it.<p>
That sets a profoundly low standard of proof for even the most serious cases--including death penalty cases.<p>

