<font face="Times New Roman, Times, serif" size="3">Kenneth admits being the driver of a car with three passengers, including Mauriceo Brown, on the night in 1996 that Brown shot and killed Michael LaHood. But he insists he had no idea a murder was going to take place.<p>
The other men involved with the crime support Kenneth's claim. During an appeal, one of the other passengers, Dwayne Dillard, testified that Kenneth tried to drive from the scene after hearing gunshots. One juror claimed he would have voted for a different verdict if he were given this information at the original trial.<p>
The judge in Kenneth's case also instructed the jury that they could find Kenneth guilty solely on the basis of his association with Brown--instructions that fly right in the face of the letter and intent of the already flawed Law of Parties.<p>
The prosecution also fanned the flames of racism by branding Kenneth a vicious gang member, and reading rap lyrics supposedly penned by him. Like so many African American men unable to afford a competent defense, Kenneth was railroaded onto death row by a Texas injustice system bent on maximizing convictions.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">FROM DEATH row, Kenneth has helped lead the struggle against the death penalty. The DRIVE Movement that he co-founded regularly stages nonviolent protests against the death penalty and an horrendous living conditions on Texas' death row. As a result, Kenneth and other DRIVE members have faced beatings by prison guards and had riot gas used against them.<p>
While news of an execution date is devastating, Kenneth and his supporters aren't backing down. Abolitionists are working to form a broad coalition to pressure Texas Gov. Rick Perry and other officials to grant Kenneth clemency and a new trial.<p>
While Perry typically ignores the demands of the anti-death penalty community, the national tide against capital punishment has made it harder for Texas to continue executing people with impunity. Recently, the state lost three major death penalty cases before the Supreme Court. National support for the death penalty continues to drop as concerns about innocence and lethal injection grow.<p>
Kenneth's supporters hope to ride this momentum with a petition and letter-writing campaign, plus a series of rallies and public events. A broad coalition, led by Kenneth's family, the Campaign to End the Death Penalty, the ISO and other groups, intends to expose the Law of Parties for the sham that it is--and put another dent in the armor of the Texas death machine.<p>

