<font face="Times New Roman, Times, serif" size="5"><b>150 years after India's mutiny against Britain</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 25, 2007 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">MAY 10 marked the 150th anniversary of the massive Indian revolt against British rule, a historic day for the anti-colonial struggle in South Asia and for rebellions against occupation everywhere. The Indian government is trying to hijack the celebration of the revolt in order to rewrite history, but we socialists have our own proud tradition of celebrating it.<p>
In 1857, the revolt (often called "the Indian Mutiny") began with an Indian soldiers' mutiny from the Bengal Army at Meerut. Soon after, they--and the civilians who joined them--entered Delhi, declared the end of the British Raj, and placed on the throne an unwilling Bahadur Shah II, the nominal heir of the old Mughal Empire<p>
Until April 1859, when the last guerrillas were crushed or driven away, the pattern of mutiny-rebellion was reproduced around the country, claiming the allegiance of more than 50 percent of the army, and the support of peasant masses and ruling elites across caste, religious and regional lines.<p>
Despite the tremendous solidarity of the rebels, the revolt ultimately suffered from great weaknesses: the failure of soldiers in the Bombay and Madras armies to mutiny, the inferiority of arms, the lack of military leadership (most officers in the Indian army were British), the absence of a clear political agenda and the breakup of fragile cross-class and cross-communal alliances.<p>
Nevertheless, the impact of the revolt was far-reaching, both in terms of British repression and Indian resistance.<p>
On the one hand, it was after the revolt that the British government officially took over from the East India Company. All sorts of changes were accelerated, like the building of an India-wide railway system to facilitate the rapid deployment of troops.<p>
On the other hand, despite the fact that many elites like the early Bengali nationalists among the intelligentsia saw the revolt as "backward," it remained--and has remained--as an example of collective heroism.<p>
The revolt has many lessons for us today, including how we view the anti-imperialist violence of oppressed groups, like those resisting Israeli occupation in Palestine and U.S. occupation in Iraq.<p>
The racist and jingoistic press in Britain justified every single act of brutality during the British counter-insurgency in light of the ferocious rebel offensives. Against this, a reporter named Karl Marx, the London correspondent for the <i>New York Daily Tribune</i>, showed what an internationalist defense of anti-imperialist struggle really means.<p>
In an article called "The Indian Revolt" in September 1857, Marx argued: "However infamous the conduct of the sepoys, it is only the reflection, in a concentrated form, of England's own conduct in India...There is something in human history like retribution; and it is a rule of historical retribution that its instrument be forged not only by the offended, but by the offender himself."<p>
In another article investigating how the British used torture as policy, Marx concludes: "We have here given but a brief and mildly-colored chapter from the real history of British rule in India. In view of such facts, dispassionate and thoughtful men may perhaps be led to ask whether a people are not justified in attempting to expel the foreign conquerors who have so abused their subjects.<p>
"And if the English could do these things in cold blood, is it surprising that the insurgent Hindus should be guilty, in the fury of revolt and conflict, of the crimes and cruelties alleged them?"<br>
<b>Pranav Jani</b>, Columbus, Ohio<p>

