<font face="Times New Roman, Times, serif" size="5"><b>Dahlia Wasfi's fight for Iraq</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 25, 2007 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">RECENTLY, I had the wonderful opportunity to hear Dr. Dahlia Wasfi discuss health care implications of the U.S. war on Iraq. Of all the many presentations I have seen on Iraq, hers was the most powerful and entertaining.<p>
Dr. Wasfi was speaking in Thousand Oaks, Calif., at an event sponsored by supporters of Global Exchange of Ventura County. She dazzled the audience with a slide show of humor and horror, both enraging and enlightening.<p>
Slides showed photos of Falluja hospital doctors tied up on the floor, U.S. soldiers towering over them; a baby without arms born to a Gulf War veteran; a wounded Iraqi who died simply because the hospital ran out of stitches.<p>
Dahlia Wasfi was inspired to visit her family in Iraq for the first time in almost two decades after the death of Rachel Corrie, who was crushed by a home-demolishing bulldozer in the Gaza Strip. Her observations and medical background form the basis of her presentation.<p>
A smattering of jokes and levity helped sweeten the bitter pill of truth, and Wasfi made seemingly outrageous comparisons to convey the monstrosity of U.S. violence against Iraq. "I cannot stand three consecutive days of rain. We bombed them for 42 days," she said, referring to the 1991 Gulf War.<p>
Sanctions against Iraq, first levied on the 45th anniversary of the bombing of Hiroshima, along with devastation of the country's infrastructure and contamination from depleted uranium munitions used by the U.S., resulted in a 600 percent increase in infant mortality rates and a 300 percent increase in infant leukemia--which was "worth it" to Madeleine Albright when she was Secretary of State during the Clinton administration. "Sewage runs in the streets to this day," Wasfi said of outbreaks of cholera in 1998, 2002 and 2003.<p>
The city of Falluja became a "free-fire zone" as the U.S. military broke international law by surrounding the hospital in search of terrorists. Wasfi asserted that no insurgents remained in the city after the U.S. invaded. "The ones who are left behind are the same ones who are left behind in New Orleans" when Hurricane Katrina hit: the poor, the sick, the elderly, and the immobile.<p>
Wasfi's examination of the inhumane cost of war wasn't confined to Iraq. Israel dropped over 1 million cluster bombs on Lebanon, 20 percent of which do not detonate. Children mistake the bright yellow canisters for food packets.<p>
The lessons of occupation and resistance aren't confined to Iraq, either. In Vietnam, in Lebanon, in Somalia, "We trained the rest of the world that the only way to get us out is to kill us."<p>
Wasfi doesn't think politicians like Barack Obama will end the war. Instead, she started and finished her presentation with a call to support the resistance of U.S. soldiers to military occupation. In particular, she encouraged people to join or donate to Iraq Veterans Against the War and Courage to Resist.<p>
Dahlia's spirit is as refreshing as it is sobering, with an infectious energy. A friend mentioned to me afterward, "I feel like I haven't been doing enough"--even though he goes to every protest in L.A.<p>
Check out Dahlia Wasfi's Web site at <a href="http://www.LiberateThis.com" target="_blank">www.LiberateThis.com</a>. I look forward to attending her presentation at <a href="http://www.socialismconference.org" target="_blank">Socialism 2007</a> in Chicago on June 14-17, and getting inspired all over again. <br>
<b>John Osmand</b>, Ventura, Calif.<p>

