<font face="Times New Roman, Times, serif" size="5"><b>Crisis for U.S.-backed regime in Pakistan</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 25, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">A CONTROVERSIAL suspension of Pakistan's top judge has mushroomed into the biggest threat yet to the country's U.S.-backed military regime.<p>
Attacks on anti-government protesters in the capital of Karachi on May 13 and 14 left 45 dead and 159 wounded. The violence came not from police--who largely stood by--but from the private thugs of a political party allied to the government. But far from intimidated, the movement launched a general strike on May 15 that brought most major cities to a standstill.<p>
Pakistan's general-turned-president, Pervez Musharraf, is a linchpin of the U.S. government's bid to control Central and South Asia. After the September 11, 2001, attacks, Musharraf, who seized power in 1999, was given a choice by the Bush administration: Pull the plug on Pakistan's support for the Taliban regime in neighboring Afghanistan, or risk being toppled himself.<p>
Musharraf complied, but ousting the Taliban angered two key forces--elements in the military and secret service that profited from dealings in Afghanistan, and ethnic Pashtun tribes that straddle the border and have autonomy from Pakistan's central government.<p>
Today, with the U.S.-NATO occupation of Afghanistan in crisis amid a Taliban resurgence, Musharrraf has had to zigzag repeatedly--ordering military attacks on some Taliban-tied tribal groups to appease U.S. demands for a get-tough policy, while making peace deals with other tribal leaders to try to maintain political influence in the border region.<p>
Added to this mix are demands for genuinely free presidential elections, supported by a range of forces, from genuine democrats to the Pakistan Peoples Party and the Pakistan Muslim League, the corrupt, rival parties dominated by the wealthy that have joined forces in the anti-Musharraf movement.<p>
Significantly, some of the main Islamist parties, which have by turns confronted and collaborated with Musharraf, have joined the movement to protest the suspension of Supreme Court Chief Justice Iftikhar Chowdhry, supposedly for corruption but in fact for rulings against the government.<p>
To counter the movement, Musharraf has armed members of the MQM party, a group based among the Mohajirs, immigrants from India who came in the 1948 partition of the country that created Pakistan. Having long faced discrimination and sometimes violence from the dominant Sindhi and Punjabi populations, many Mohajirs look to the MQM for political and physical protection. Like other Pakistani rulers before him, Musharraf harnessed this dynamic to carry out last week's repression against his opponents.<p>
Musharraf faces a year-end deadline to resign as commander-in-chief of the military under Pakistan's constitution. Whatever happens, the street battles are sign that the crisis of U.S. imperialism has become a crisis for its puppets, too.<p>

