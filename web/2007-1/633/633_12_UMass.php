<font face="Times New Roman, Times, serif" size="3">Card knows very well that he faces opposition on campus--when he gave a lecture in April, he faced a protest that he acknowledged in his speech.<p>
When Card was informed by administrators of the opposition on campus to his degree, he contacted a faculty member, Paula Chakravartty, involved in organizing the protests against him to complain.<p>
Chakravartty said Card insisted that he was a "nice guy," and that protesters didn't know anything about him personally. When Chakravartty explained that the opposition wasn't personal, but because of his public record, particularly his role in selling the Iraq war, his only response was: "I have been invited to receive this great honor, and I am not rude."<p>
On May 17, the UMass faculty senate met to consider a motion to oppose Card's degree--putting them on record along with the the Student Government Association and Graduate Student Senate. The Senate vote to revoke the degree 31-0, but it was declared void because the body was one vote of having enough senators present for official business to be conducted--the result of the one pro-Card senator leaving the meeting rather than vote against the motion.<p>
The protests against Card won't stop. Momentum is building for a week's worth of events, with a rally planned for May 22 on the UMass campus, followed by a protest at President Jack Wilson's office in Boston the next day--all leading to a protest of Card at the commencement on May 25.<p>
By awarding an honorary degree to a man completely without honor, the UMass administration has stirred protests by hundreds of students, faculty, staff and community members. All members of the UMass community are drawing links between the Iraq war that Card championed--and a campus administration that gives itself raises while hiking fees and slashing opportunities for working-class students.<p>

