<font face="Times New Roman, Times, serif" size="5"><b>Honorary degree for a man with no honor</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Charles Peterson and Sarah Beladi</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 25, 2007 | Page 12</font></font><p>

<font face="Times New Roman, Times, serif" size="3">THE UNIVERSITY OF Massachusetts administration's plan to award former White House Chief of Staff Andrew Card with an honorary degree has sparked protests of hundreds over the last two weeks, with plans for further action as <i>Socialist Worker </i>went to press.<p>
The honor for Card is widely opposed on campus because of Card's role on the White House Iraq Group, which concocted the strategy to sell the disastrous invasion of Iraq to the public.<p>
More than 500 people gathered for a rally May 15 after the administration refused to change its position on the award for Card. Fearing a repeat of the previous week's demonstration, when students marched through the Whitmore administration building, chanting angrily, and dropped off hundreds of signatures on an anti-Card petition, the administration shut down the building.<p>
Denied entry to the administration building, the more than 500 protesters marched to the Goodel building, home of the offices of the dean of the graduate school. The demonstrators demanded to meet with the dean and sat in for roughly half an hour. When police were called in, they protesters marched out of the building and returned to Whitmore to continue rallying.<p>
At Whitmore, the protesters encountered Vice Chancellor Mike Gargano, who was forced to make a statement on whether administrators had reconsidered their position. He said they would take more petitions, but "the decision...remains the same." To this, the crowd responded with a deafening chorus of "Resign! Resign!"<p>

