<font face="Times New Roman, Times, serif" size="5"><b>Locked out at NYC's Saigon Grill</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 25, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">DELIVERY WORKERS at New York City's Saigon Grill restaurants were locked out March 2 after they began protesting low wages and unsafe working conditions. Even though workers are paid as little as $1.60 per hour, management charges delivery workers outrageous fines for "infractions" such as for taking a sick leave or slamming the door.<p>
Employees say that the owners harassed delivery workers, calling them "dirty dogs," and put them in dangerous situations delivering to unsafe buildings. When a worker is robbed and beaten, they have to pay for all the orders they deliver that day.<p>
Saigon Grill worker are receiving the support of the community, including students who have organized pickets and other actions to protest the restaurant's poor treatment of its employees. YU GUAN KE, who had worked for Saigon Grill for more than 10 years before being locked out, talked to<i> Socialist Worker's</i> PETER LAMPHERE.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>IS SAIGON Grill a typical employer or worse than normal?</b><p>

IN TERMS of wages, Saigon Grill is typical. The untypical thing about Saigon Grill is their fines--their $20, $50, $200 fines--which is basically a way to steal workers' tips. Just as restaurants will steal waiters' tips, the fines are just another way of taking away their tips.<p>

