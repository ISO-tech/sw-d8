<font face="Times New Roman, Times, serif" size="3">They're supposed to pay us the minimum wage for tipped workers, but we didn't know that. Even though we get tips, we're supposed to make a minimum wage of $4.60 an hour--for non-tipped workers, it's $7.15--but [the owner] never paid us minimum wage.<p>

<b>WHERE DID you get the idea that delivery workers should organize together?</b><p>

BECAUSE THE AAA 88 Palace restaurant in Chinatown won their case. We read an article about their case in the newspaper. That's why we wanted to organize at Saigon Grill.<p>
There were two restaurants--one was AAA, and the other was Green Ming Dynasty. They were both groups of waiters who won victories against tip stealing. That's how we heard that we should be paid minimum wage.<p>

<b>MANY WORKERS in this situation might choose an individual solution and go to their boss by themselves. Why did the workers at Saigon choose a collective solution?</b><p>

BECAUSE IT wasn't just about the wages. We were just so abused in the restaurant, all of us--the way they treated us, the way they yelled at us and harassed us. They gave us leftover food to eat. This was the common problem we had.<p>
When I saw the newspaper, I realized that for 10 years, he had been cheating me of my minimum wage. So when we found out that it was possible that people could stand up and win, we decided to fight.<p>

<b>WHAT WAS the reaction of other workers at Saigon Grill when the delivery workers got fired?</b><p>

WE DON'T know, because when we had a meeting with the owner and he tried to make us sign a paper that fired us, everybody had left for the night, and we haven't been able to go back inside.<p>
Right after the owner fired us, he raised wages for the inside workers and shortened their hours. Even if the people inside have had problems with the owner before, now he raised their wages.<p>

<b>WHAT HAS been the effect on other restaurant workers in the neighborhood?</b><p>

IN RESTAURANTS all over Manhattan, employees are either suing the owner like we did--like at Republic or Ollie's. Other workers have come to join our picket lines. At many restaurants, they're afraid of workers doing what we did, so they're raising wages and shortening hours.<p>
We have reached out to a lot of places, and politicians, students and all types of workers are coming out. We have been raising awareness of the situation for delivery workers.<p>

