<p><a name="Police"></a><font face="Times New Roman, Times, serif" size="4"><b>Who polices the police?</b></font><p>

<font face="Times New Roman, Times, serif" size="3">REMEMBER THE three Atlanta police officers who killed a 92-year-old woman who was just defending her home? A grand jury has indicted them for something other than murder, but perhaps at least they will receive some sort of punishment.<p>
Every city, county and state should have police review boards. We citizens need to stand up against police brutality, corruption and abuse, unless we want to live in a police state.<br>
<b>Chuck Mann</b>, Greensboro, N.C.<p>

