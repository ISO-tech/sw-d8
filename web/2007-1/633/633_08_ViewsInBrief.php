<a name="Workman"></a><font face="Times New Roman, Times, serif" size="4"><b>Philip Workman's last request</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SOCIALISTS ARE often told that a society without war, exploitation and poverty is impossible because it is human nature (whatever that is) to be "greedy." In the face of one of the most reprehensible aspects of our current society--our criminal justice system--that argument recently was shown to be patently absurd.<p>
The state of Tennessee executed Philip Workman on May 10. Like hundreds of other people murdered by the state since the death penalty was reinstated in 1976, Workman's case was surrounded by doubt. The ballistics evidence indicated that Workman's gun couldn't have been the murder weapon. The prosecution's lead witness later recanted his testimony, admitting he took the stand because he needed the money offered by the DA to support his drug habit.<p>
The state of Tennessee callously brushed these doubts aside, however, and executed Workman nonetheless. The state even went so far as to deny Workman's last meal request, which was to have a vegetarian pizza delivered to any homeless person in Nashville.<p>
The day of Workman's execution, however, homeless shelters all around the state reported a flood of pizza donations coming in from across the country. Word had gotten out about Workman's final request, and people had taken it upon themselves to do what the state would not.<p>
One woman called her friends and managed to gather $1,200 to donate for pizza. "I put some of it on the credit card," she said, "But I thought we'll find a way to pay for them later. I just felt like I had to do something positive."<p>
While we are constantly told how inherently greedy people are, the actions of a few who were moved by our current system's barbarism prove otherwise. If one woman and some friends can raise $1,200 to give Tennessee's homeless a pizza party, imagine what our entire society can do once we put human need before corporate greed.<br>
<b>Paul Heideman</b>, Madison, Wis.<p>
