<p><a name="Aim"></a><font face="Times New Roman, Times, serif" size="4"><b>Taking aim at immigrants</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ARMED LAW enforcement officers walking down the aisle of train, demanding to see people's papers, sounds like a scene from a movie set in a totalitarian state--but it's not.<p>
Every night, around 11 p.m., the westbound Amtrak "Lakeshore Limited" stops in Rochester, N.Y., on its way from New York City to Chicago. There, agents of the U.S. Border Patrol board the train and sweep from end to end, racially profiling passengers.<p>
The agents question those passengers of obvious non-white ethnic backgrounds, demanding proof of citizenship (as if everyone carries their social security card or their passport around with them). They search the train from end to end, hunting for undocumented workers, and delaying the train until they are satisfied that no one is without proper papers.<p>
On the night of May 7, at about 11:30 p.m., two Border Patrol agents forcefully removed a Black male from the train, his only apparent crime being Black in America.<p>
The crackdown is part of a larger crackdown on the immigrant community, a part of the backlash against them for standing up for their rights across the U.S. in last year's massive May Day demonstrations. Border Patrol and Immigration and Customs Enforcement agents, all part of the Department of Homeland Security, have been raiding workplaces and sweeping transit centers, isolating individual immigrants and detaining them.<p>
In this way, when people are alone, the government can intimidate the immigrant community--because they dare not challenge the large crowds of protesters, people raising their voices against the inhumane Sensenbrenner bill or the STRIVE Act. This sort of random intimidation is guaranteed to continue under any legislation that expands the criminalization of immigrants.<br>
<b>Josh Karpoff</b>, Rochester, N.Y.<p>
