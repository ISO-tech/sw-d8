<font face="Times New Roman, Times, serif" size="5"><b>Resister released but still not free</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 4, 2007 | Page 5</font><p>

MILITARY RESISTER Agust&iacute;n Aguayo was released from confinement at a U.S. military base in Mannheim, Germany, on April 18, but he's still far from free.<p>
"We thought he'd be home soon, but that hasn't happened," his wife Helga wrote in an e-mail to a supporter.<p>
After trying to get conscientious objector status for nearly three years and facing his second deployment to Iraq, Agust&iacute;n went absent without leave from the Army in late 2006. In March, a court-martial proceeding found Agust&iacute;n guilty of desertion and missing movement. He was sentenced to eight months' confinement--and therefore was released after two months because he had already spent six months in detention.<p>
Helga and his twin daughters thought he would be headed home, but now the Army says that it plans to keep Agust&iacute;n on active duty for one to two years more. And he remains under the authority of members of his old unit--the same "people that tried to take him by force, i.e., shackle, handcuff and carry him onto the plane" for his second Iraq deployment, explained Helga.<p>

