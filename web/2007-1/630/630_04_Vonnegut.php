<font face="Arial, Helvetica, sans-serif" size="2"><b>VIEWS AND VOICES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A critique of the "good war"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 4, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">THE PASSING of Kurt Vonnegut was a reminder of the impact his novel <i>Slaughterhouse-Five</i> had on me when I first read it after I completed my service in the United States Marine Corps.<p>
Published in 1969, the work appeared at the height of the Vietnam antiwar movement and was embraced by activists as a scathing critique of both war and the consumer culture of U.S. society.<p>
<i>Slaughterhouse-Five</i> challenges the memory of the Second World War as the "good war" by recalling the brutal firebombing of Dresden by Allied forces. Billy Pilgrim, the work's narrator and a semi-autobiographical character, survived as a prisoner of war during the firebombing, as did Vonnegut himself.<p>
Billy goes back and forth through time and fantasy to recall the horrific scenes and brutality of the Second World War, intermixed with sequences of his boring middle-class existence in consumerist America. Meanwhile, he often drifts into moments of fantasy, as he believes he has been kidnapped by aliens, the Tralfamadorians, who impart to him the meaning of the universe.<p>
Throughout the work, Vonnegut highlights what he perceives as the corrupting consumer culture of U.S. society. Vonnegut juxtaposes the materialism prevalent in Billy's family to the moon-cratered aftermath of the destruction of Dresden. From Barcaloungers and Coca-Cola, to Ford El Dorados and Rambler Rose silver platters, Vonnegut purposely interjects these fad items into the narrative.<p>
When Billy slips back into his memories of the war, the death of his fellow soldiers, the extermination of young children and his witness to the boiling of flesh, these quirky and mass-consumer novelties seem grotesque and jarring. Billy's family is more concerned with the prevailing culture of conformity than to ponder the deeper meanings of life that Billy explores.<p>
Billy searches for alternative values to make sense of the horrific war that he experienced. Yet at times, a dark cynicism weaves through the work, a feeling often common to veterans who return from war and feel alienated from what they perceive as the inane banalities of the civilian world.<p>
In <i>Slaughterhouse-Five</i>, the brutality of war becomes farce; however, Vonnegut himself was by no means a cynic. He earned a reputation as an opponent of war and injustice, both in his literature and as a public speaker who participated in movements for social change.<p>
Vonnegut's work should be read by all not only as a potent reminder that the so-called "good war" was profoundly tainted and corrupt, but also as a testament to Vonnegut himself--a writer for our side who will be deeply missed.<br>
<b>Martin Smith</b>, Champaign, Ill.<p>

