<a name="Death"></a><font face="Times New Roman, Times, serif" size="4"><b>California's secret death chamber</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SO THE governor of California is building a secret death chamber to start to comply with federal judge [Jeremy] Fogel's order to fix how the state executes people. What's next? Secret executions? Secret trials?<p>
The Democrats are in an uproar about the secrecy, although I suspect some just wanted to be asked for their approval, rather than to slow or stop the machinery of death.<p>
The governor used prison labor for this project, so now we know how they were going to attempt to build it for a mere $399,000 (under the amount that needs legislative oversight). Slave labor is what made this country thrive, and it continues to do so.<p>
The building was stopped, and they maintain it wasn't because they were caught, but because of a cost overrun of $300,000--almost double what the death chamber was supposed to cost. We know there is no right way to do the wrong thing. Let's continue the fight to stop them from building structures of hate and violence.<br>
<b>Pat Foley, </b>Oakland, Calif.<p>
