<p><a name="Duke"></a><font face="Times New Roman, Times, serif" size="4"><b>Media forgetting Duke players' racism</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NOW THAT criminal charges have been dropped against the athletes at Duke University who were suspected of raping a Black woman, the media are treating them as if there was never any doubt that they were "innocent victims." But it's worth remembering the racist pattern of behavior that these students engaged in.<p>
For one, police seized an e-mail sent out before the alleged rape by Duke student Ryan McFadyen, stating, "I've decided to have some strippers over and all are welcome. I plan on killing the bitches as the [sic] walk in and proceed to cut their skin off while cumming in my duke spandex." McFadyen's attorney Glen Bachman admitted that "the language of the e-mail is vile," but foolishly argued that it "demonstrates that its writer is completely unaware that any act or event remotely similar to what has been alleged ever occurred."<p>
Later on during the night of the incident, 911 got a call from a Black college student out walking with her friends who was called a "nigger" as she walked past the team's house. "Thank your grandfather for my nice cotton shirt," was another epithet shouted by one of the Duke students. Someone also reportedly yelled that they had asked for white dancers, not Black ones.<p>
Furthermore, the doctor who examined the victim said that her body showed signs that she was raped. A cab driver that picked up one of the alleged attackers overheard the student loudly saying, "She's just a stripper."<p>
Add to that the racism of the U.S. justice system, which never convicted anyone in the lynching death of young Emmett Till, despite re-opening the case recently. Thirty years passed before someone was convicted in the death of civil rights leader Medgar Evers.<p>
If there is ever going to be justice in this country, we're going to need a new justice system--one that's not founded on bigotry.<br>
<b>Dominic Renda</b>, New York City<p>
