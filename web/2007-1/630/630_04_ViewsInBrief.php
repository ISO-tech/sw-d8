<p><a name="Women"></a><font face="Times New Roman, Times, serif" size="4"><b>The real role of women in Islam</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AS A Muslim, I read with interest and general approval your article "Standing Up to Islamophobia." (April 20) However, I would dispute the characterization in the piece that, "As in all religions, elements of Islam are explicitly conservative--for example, the attitude that women are the inferiors of men."<p>
While such a view is indisputably prevalent in currently existing Muslim societies, it should be acknowledged that the Koran and the actions of the prophet Muhammad support a view of women as fundamentally equal to men.<p>
It is clear in the Koran that women are equally endowed as men with the ability and responsibility to engage in governance of the community, to participate in economic life (on her own initiative without interference from husbands or other male relatives), and to participate in education.<p>
In short, the Muslim woman has the right and duty to perform all social and religious activities incumbent upon her male peers. In the early Muslim community, women exercised these rights--often against the opposition of males. No Muslim jurist in the early formative period of Islam ever endorsed the view of women as soulless animals that was common among the early Christian church fathers.<p>
If one wishes to build bridges and make common cause with the oppressed Muslims around the globe, it would be helpful if socialists would not assume that all ancient religious traditions are the same and project the deficiencies of Christianity upon Islam. It is a bit akin to blaming the International Socialist Organization for the crimes of Stalinism.<p>
Which is not to say that the Koran and authentic Muslim religious law promotes the same conception of women as modern secular socialist feminists do. There are indeed deep disagreements between the two views that cannot be easily disregarded or reconciled.<p>
I would invite the editors and contributors to your publications to learn more about the history of Islamic social thought. Of particular interest to your readers would be the Koranic and legal discourse concerning the distribution of wealth in society that prefigures the arguments of socialist thought that only appear in the West after the Enlightenment.<br>
<b>Hasan McCormick</b>, from the Internet<p>

