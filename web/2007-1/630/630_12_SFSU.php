<font face="Times New Roman, Times, serif" size="5"><b>Students walk out at SFSU over fee hike</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Kristin Lubbert</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 4, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">ABOUT 700 San Francisco State University (SFSU) students walked out of classes April 26 to protest a fee increase planned for the upcoming school year.<p>
Since 2002, student fees have increased by 91 percent in the California State University system--and the state plans to continue raising them by 10 percent every year until 2011. This has obviously not gone unnoticed by students who bear the burden of the increases.<p>
On the morning of the protest, some 300 students gathered for a rally on Malcolm X Plaza, then began marching around campus and into buildings, chanting, "March today, take a stand, lowered fees is our demand!" and "Walkout! Walkout!" Some 400 people joined the protest and marched to the administration building.<p>
While students have had their pockets picked by the fee increases, SFSU President Robert Corrigan has gotten continual pay raises in the past few years. His current salary is $261,144, he gets free housing, and he receives a $12,000 car allowance.<p>
Representatives from Associated Students and a student organizer of the California Faculty Association tried to slow things down and prevent people from entering the administration building, but were ultimately unsuccessful.<p>
Around 500 people charged into the building and made their way to the fifth floor, where Corrigan's office is located. All entrances to the fifth floor were locked, so students reconvened and held a sit-in in the lobby.<p>
Some of the more conservative forces took over the sit-in and attempted to justify why they tried to prevent us from entering the building. But once the bullhorn was opened up to the hundreds of students sitting in, the anger against fee hikes began pouring out.<p>
Most students spoke about working nearly full time and taking out massive loans just to get through school. A mother of two explained how she had to work the graveyard shift, go to school during the day and watch her kids in the evening in the hopes of getting a higher-paying job.<p>
A representative of Students Against War spoke about how the hundreds of billions spent to occupy Iraq is linked to cuts in public education. Still other speakers pointed out that the state is hiking fees at the same time as it is planning the largest single prison construction program in U.S. history, with a price tag of some $8.3 billion.<p>
Planning for the walkout began several weeks ago among a small group of activists. The three demands of the action were a rollback of fees to the 2002 level, no cuts in the Educational Opportunity Program, and more classes offered by the school.<p>
The demonstration was larger than expected, embodying the growing anger on campus. It was also pretty disorganized, but hundreds of the sit-iners wanted to attend a follow-up meeting planned for this week. Students are also planning to travel to Sacramento to lobby the state legislature for more funds.<p>
The best way forward is democratic meetings where students are allowed to decide the next steps--so that next time, we can be even more effective in challenging the administration.<p>

