<a name="Abortion"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend abortion rights</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Brian Lenzo</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ROCHESTER, N.Y.--More than 175 people lined the street at a busy intersection here April 25 to protest the latest ruling by the Supreme Court that upholds a federal ban on the intact dilation-and-extraction abortion procedure, misnamed "partial birth" abortion by the right.<p>
The demonstration was called by Planned Parenthood, and about equal numbers of men and women turned out to carry signs that read "Stop the war on women," "Support the Freedom of Choice Act" and "Our lives, our bodies." Organizers led the crowd in a loud chant of "Not the courts, not the state--women should decide their fate!"<p>
Many saw the Freedom of Choice Act that's currently proposed in the House and Senate as crucial to keeping abortion legal. While women's right to abortion should certainly be codified into law, the fight to make that happen, while also guaranteeing access to abortion, can't happen solely in the halls of Congress. It will take building a movement in the streets.<p>
This rally shows the potential for rebuilding that kind of movement, and was a great first step in building a new fight for abortion rights.<p>
