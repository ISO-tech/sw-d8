<p><a name="FAIR"></a><font face="Times New Roman, Times, serif" size="4"><b>Protesting the FAIR bigots</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jeff Skinner</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WASHINGTON--Some 50 immigrant rights activists came out on April 22 to confront a group of anti-immigrant bigots staging a rally in a park in front of the White House.<p>
The bigots' rally, organized in part by the Federation for American Immigration Reform (FAIR), featured speakers spouting such racist trash as "Illegal immigration should have stopped on September 12, 2001."<p>
The counter-protest kept up a steady barrage of chants and heckling throughout the bigots' demonstration. Although antiracist protesters were initially kept out of the park by police, the group eventually made its way back into the park and much closer to the speakers' platform, where they continued to disrupt the proceedings with more chants, whistles and air horns.<p>
People left the protest energized and ready for the upcoming immigrant rights demonstration on May 1. Coco Magellanes of the group M&eacute;xicanos Sin Fronteras linked the protest with the ongoing fight for immigrant rights. "Anything people do to support immigrant rights will help bring about a recognition of those rights," Magellanes said. "We have to build not only demonstrations like May Day, but the civil rights movement as a whole."<p>
