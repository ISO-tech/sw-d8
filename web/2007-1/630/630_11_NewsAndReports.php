<p><a name="Sweats"></a><font face="Times New Roman, Times, serif" size="4"><b>No sweats struggle at University of Washington</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Steve Leigh</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--Chanting "DSP! Make the UW sweat-free!" 75 students marched through the University of Washington (UW) campus April 27 to put pressure on UW president Mark Emmert.<p>
The activists are demanding that UW join the "Designated Supplier Program" (DSP). This would ensure that all apparel with the UW logo would be made in shops where workers have the freedom to form unions and earn a living wage.<p>
Emmert at first seemed to be moving toward joining the DSP, but recently backed away. The Student Labor Action Project (SLAP), which organized the rally and march, is planning future actions to force UW to join the DSP.<p>
This march coincided with "Washington Weekend" when future students, parents, alumni and the public flock to the campus. Hundreds lined up for free hamburgers flipped by Emmert--while SLAP rallied, marched and leafleted the crowd.<p>
So far, 30 universities and colleges have signed on to the DSP, including Western Washington State University and Seattle University. Students hope this will put pressure on the UW to do the right thing as well.<p>
E-mail <a href="mailto:slap@jwj.org">slap@jwj.org</a> for information on how you can form a similar campaign at your college or university.<p>

