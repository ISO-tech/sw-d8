<p><a name="UC"></a><font face="Times New Roman, Times, serif" size="4"><b>Military-free zone at UC-Santa Cruz</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Alessandro Tinonga and Jessica Shakarian</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SANTA CRUZ, Calif.--Students Against War (SAW) at the University of California-Santa Cruz (UCSC) held a rally April 24 to celebrate military recruiters canceling their plans to attend the campus' Spring Job Fair.<p>
For the past two years, student protests have stopped military recruiters from advertising on the campus. While protesters were prepared to take on the recruiters once again, the military instead decided not to show up. Despite claims by the recruitment center that they were "overbooked," SAW believes their cancellation is a clear indication that protests have effectively made UCSC a "military-free zone."<p>
To celebrate, approximately 100 people turned out for a rally, with members of Iraq Veterans Against the War (IVAW) leading the way. Starting at Quarry Plaza in the center of the campus, protesters marched around campus, ending in front of the chancellor's office, where they held a memorial for the fallen men and women in Iraq.<p>
Various students and IVAW members spoke on topics ranging from the possibility of war with Iran; to the corporations who come to career fairs to recruit students; to the ties that UCSC holds with aerospace giant Lockheed Martin to help manage nuclear labs.<p>
This shows that protest by students can keep the military from using our campuses to recruit more men and women to kill and die for oil in Iraq.<p>
