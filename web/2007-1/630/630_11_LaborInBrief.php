<p><a name="UVM"></a><font face="Times New Roman, Times, serif" size="4"><b>Student Labor Action Project at UVM</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jessica Zamiara</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BURLINGTON, Vt.--As many as 200 students, workers and faculty gathered in support of livable wages at the University of Vermont (UVM) in a rally on April 27 that marked the end of a five-day hunger strike by the Student Labor Action Project (SLAP).<p>
Nancy Welch, a representative of United Academics, pointed to the skyrocketing salaries of top UVM officials. "Today," she said, "there are 15 people with salaries above $200,000--including [university President Dan] Fogel's, which has gone from $260,000 in 2003 to $301,144 today," Welch said. "How would you like a $41,000 raise?"<p>
As part of its livable wage campaign, SLAP is demanding a base wage raise to $13.62 an hour. When the SLAP hunger strike started, the administration immediately expressed concern for the student strikers' health. "It is unfortunate they are focusing so much on our safety, and not the issue at hand," said hunger striker Alessandro Ascherio.<p>
The rally came to a close with SLAP taking their demands directly to the administration. Chanting "Raise the floor!" and "Hey hey, ho ho, these poverty wages have got to go!" protesters filed into the administration building, where administrators had locked themselves in their offices.<p>
A meeting between the SLAP hunger strikers and Fogel ended with a verbal agreement that Fogel will re-launch the basic needs and equitable compensation task force. SLAP members considered this a victory on many accounts.<p>
"We began following a denial by the administration that compensation for underpaid workers on this campus was an issue," the group explained in a statement, "and we ended with a commitment by the administration to continue working on this issue and a videotaped statement by President Fogel that the livable wage issue will be significantly considered moving forward."<p>
"This is not the end," the SLAP statement continues. "We will continue to promote and defend the rights of the under-compensated workers on this campus, today, tomorrow, and into the future."<p>

