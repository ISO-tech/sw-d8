<a name="Hayward"></a><font face="Times New Roman, Times, serif" size="4"><b>Hayward, Calif., teachers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By John Green</b></font><p>

<font face="Times New Roman, Times, serif" size="3">HAYWARD, Calif.--Public school teachers here ended their 10-day strike after forcing substantial concessions from the school district. By a 9-to-1 margin, teachers approved a tentative agreement that calls for an immediate 8 percent raise, a further 3 percent raise next year, and a one-time 1 percent salary bonus.<p>
Under the new contract, Hayward teachers will surge ahead from the 14th to the eighth best compensated out of the 17 school districts in Alameda County. Though teachers lost about half a month's income during the strike, they will earn back that amount and then some even before next year's 3 percent raise kicks in.<p>
Both sides announced the agreement just hours after hundreds of striking teachers, parents and community members rallied in downtown Hayward outside of city hall. Their well-organized public action evoked a deafening response of horn blasts from passing cars and trucks.<p>
Daily unity rallies and the formation of the bilingual Families in Action solidarity group bolstered morale on the picket line and clearly contributed to the district's failure to break the strike. In the final days of the strike, the district moved for an injunction against the teachers, essentially declaring that the strike was too effective and well organized to be broken without outside help.<p>
The militant, unbending fight by Hayward teachers and parents will set a powerful precedent in the Bay Area for years to come. "We won more than just dollars," union president Kathleen Crummey said. "The Hayward community is now galvanized to save our schools and education in Hayward."<p>
