<font face="Times New Roman, Times, serif" size="5"><b>Bush backpedals on North Korea</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 23, 2007 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THE TERMS of the recent diplomatic "breakthrough" with North Korea have dealt yet another blow to the fantasies of the neoconservative hawks who once ruled the roost in Washington.<p>
After years of superheated rhetoric about dire threat posed by North Korea, the U.S. government put its stamp of approval on a deal that requires very few concessions from the government of Kim Jong Il--and puts off the contentious issues for future negotiations.<p>
The deal, brokered by the Chinese government, requires North Korea to shut down its Yongbyon nuclear reactor within 60 days in exchange for 50,000 metric tons of heavy fuel oil. If the plant is then permanently disabled, North Korea will receive an additional 950,000 tons of fuel oil.<p>
But the agreement says nothing about nuclear weapons that North Korea already has--nor does it address the supposed existence of North Korea's secret program to enrich uranium.<p>
The terms are essentially the same as those the Bush administration once rejected out of hand.<p>
In 1994, the Clinton administration negotiated a similar agreement--energy assistance in exchange for a freeze on North Korea's use of plutonium-producing reactors. But Clinton never delivered on a promise to help ease North Korea's perennial energy crisis by building two light water reactors.<p>
After September 11, North Korea suddenly found itself rounding out Bush's axis of evil. Within a year, the Bush administration ended the fuel oil shipments, provoking a crisis that led to North Korea's decision to start up its reactors again.<p>
The new agreement essentially restores the terms and agreements that existed before 2002. But there is one big difference--since 2002, North Korea has refined enough material to produce six to 10 nuclear warheads.<p>
So why did the Bush administration go along with the agreement.<p>
First, U.S. blunders in Iraq have weakened the Bush administration's hand internationally. U.S. war planners are flirting with widening the conflict to Iran, so an agreement with North Korea eliminates another potential confrontation at a time when the U.S. military is already overextended.<p>
Simultaneously, the agreement could help the U.S. neutralize China's opposition to war on Iran. By making the deal with North Korea, China is hoping to eliminate a source of instability in its backyard and expand its economic linkages. U.S. officials may hope that by going along, they will get more cooperation from China against Iran.<p>
Ultimately, though, the North Korea agreement is a demonstration of the flip side of Bush's rush to war against Iraq. The possession of weapons of mass destruction <i>lessens</i> the likelihood of a U.S. military threat--because such weapons give their possessors, such as North Korea, a credible strategic deterrent.<p>
Put another way, U.S. threats of regime change--not to mention the massive U.S. nuclear arsenal itself--create an incentive for countries unwilling to accept American dictates to pursue nuclear weapons.<p>
And it's these threats--from the world's biggest bully and the only country ever to have used nuclear weapons in a war--that make the world a more dangerous place.<p>

