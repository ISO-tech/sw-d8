<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Can ordinary people run society?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 23, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">A COMMON argument against socialism is that the majority is incapable of ruling collectively. We need educated, intelligent experts to run such a complex system.<p>
The legendary stupidity of George W. Bush, whose rich parents and crony friends bought him passing grades and much more, or the incompetence of Federal Emergency Management Agency bureaucrats during Hurricane Katrina in 2005 are both strong arguments against this view.<p>
"I think we are welcomed," said Bush when asked about Vice President Dick Cheney's predictions that U.S. troops would be greeted with thanks by Iraqis. "But it was not a peaceful welcome." When Brazilian President Luiz Ign&aacute;cio "Lula" da Silva showed him a map of Brazil, Bush exclaimed, "Wow! Brazil is big."<p>
There are many other examples that could be cited of presidents, industrialists and bureaucrats with limited, if non-existent, abilities.<p>
Most people at the very top of society, the multimillionaires and billionaires, play no direct function in its running--they merely collect the rewards of ownership. The ruling class today has become entirely parasitic, siphoning wealth, but serving no useful social function.<p>
As early as 1881, Frederick Engels wrote that the capitalists do little but cash in dividend checks. "The social function of the capitalist here," he says, "has been transferred to servants paid by wages; but he continues to pocket, in his dividends, the pay for those functions though he has ceased to perform them."<p>
"We [can] manage very well without the interference of the capitalist class in the great industries of the country," Engels concluded. "Stand back! Give the working class the chance of a turn."<p>
Bankers and investors don't make steel. It hardly takes intellectual brilliance for some who inherits a million to double it. Society could do away with the ruling class and suffer no more than when an appendix is removed from a human body.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">BUT DO workers possess the capacity to rule? Won't they still depend on experts?<p>
Often, it is workers' own hard-won, first-hand knowledge that engineers and managers use to figure out how to improve production--that is, to squeeze as much out of workers as possible.<p>
Not to deny the genius of a Newton or an Einstein, but "if science is understood in the fundamental sense of <i>knowledge of nature," </i>writes Clifford Connor in his <i>People's History of Science, </i>" it should not be surprising to find that it originated with the people closest to nature: hunter-gatherers, peasant farmers, sailors, miners, blacksmiths, folk healers and others forced by the conditions of their lives to wrest the means of their survival from an encounter with nature on a daily basis."<p>
Given the opportunity, everyone is capable of learning the scientific, administrative and mathematical skills necessary to play a direct role in running society, just as in pre-class society, knowledge of terrain, plants and animals or tool-making was shared by the group, and not treated as the monopoly of a minority.<p>
Experts and scientists would still be needed for a time even under socialism, until the education system was improved to the point that the majority received education which today is reserved only for the privileged few. For a time, workers would have to exercise democratic control over the bookkeepers, managers and engineers.<p>
But with society's vast resources diverted toward education, the distinctions between mental and manual work would break down, and the majority would be capable of doing many different kinds of jobs, from manual work to scientific work to administrative work.<p>
If workers, through their own directly elected representatives, were to seize control of production, mistakes would no doubt be made. But they would be the mistakes of the collective rather than the blind workings of the market--and could quickly be remedied by experience.<p>
For example, if the workers of Chicago ran the city instead of corporate bigwigs and their corrupt political hirelings, they would immediately begin solving the city's most pressing problems.<p>
The homeless would be quickly housed in unused homes, excess hotel space, and the requisitioned second and third homes of the rich. Meanwhile, unemployed construction workers would be organized to begin building more houses.<p>
The ill-gotten gains of the city's patricians and their hangers-on would be seized and used to provide jobs, feed the hungry, improve dilapidated schools and provide better park services, improved transportation and real after-school programs for <i>all</i>.<p>
In our society nothing is done if it isn't profitable. In a society run by the collective producers, these problems could be solved because social need, rather than the market, would determine how decisions are made.<p>

