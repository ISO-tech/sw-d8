<font face="Times New Roman, Times, serif" size="4">Two Cochabamba activists speak out:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"The right doesn't care if people die"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 23, 2007 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">BORIS R&Iacute;OS and CLAUDIA L&Oacute;PEZ, organizers with the Coalition for Defense of Water and Life in Cochabamba, talked to <i>Socialist Worker's </i>SARAH HINES and KARL SWINEHART about the significance of the confrontation in January between supporters and opponents of right-wing Gov. Manfred Reyes Villa, which left two dead and more than 100 wounded.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>WHAT IS the oligarchy trying to achieve and how?</b><p>

<b>Boris: </b>The right is playing a role of trying to destabilize things without measuring the consequences. They don't care if people die or if Cochabamba burns. Their game is to destabilize.<p>
Our enemy is the right, because the right is defending the established order, and that established order is this capitalist regime. We have to beat the right, but we can't be hypocrites either and defend the government, even if they're our brothers, when they defend the established order.<p>
The government had the possibility to direct the conflict in a different way, and it doubted itself at every step and wasn't listening to anyone. It played around, and this is the result.<p>

<b>SHOULD THE Morales government bear some of the responsibility for the outcome in Cochabamba?</b><p>

<b>Boris: </b>One of the errors was not having a line that said we want the governor out, so we're going to advance with a plan and with a legitimacy that doesn't have people going all over the place. This was an ideological, political and material defeat for us.<p>
Because of this, we have to say to the government, "You have to listen. You have to accept your limitations. You have to know to talk to the people and listen to the people." And, furthermore, don't fall into the error of saying that legality is above legitimacy. Everything we've gotten in this country has been through struggle.<p>
<b>Claudia: </b>The right has fortified itself with this last conflict. They've gained a little bit of morale at the level of mobilization and position. So people in Cochabamba find themselves still trying to understand what happened and what position to take.<p>
The role of the government has been totally wrong. You have to recognize clearly that the movements that participated in this last mobilization belong to and are aligned with the government--the coca growers and the <i>campesinos</i> [peasants] from the valleys responded to its call.<p>
So at the organic level of mobilization and logistics, they were strong in this mobilization. But they didn't have the capacity to call on the urban sectors of Cochabamba, when in December we could measure that people in the city didn't want to align with the "Half Moon" [the eastern departments whose leaders are demanding autonomy].<p>
We don't know what is going to happen yet in the future, but we do hope that at least we can begin to reconstruct our banners of liberation--and that the people can get the confidence again to feel like their voices can be heard.<p>

