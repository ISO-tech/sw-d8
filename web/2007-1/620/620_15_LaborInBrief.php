<a name="Cook"></a><font face="Times New Roman, Times, serif" size="4"><b>Fight the cuts in Cook County</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Lalasz</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--Braving subzero temperatures, dozens of registered nurses (RNs) and their supporters picketed the office of Cook County Board President Todd Stroger office on February 7.<p>
The action, organized by the National Nurses Organizing Committee (NNOC) local and dubbed "Missing: Todd Stroger," brought protesters together as a mock search party for Stroger, who has not been seen in public since January 16 when he delivered his proposed budget with its draconian 17 percent cuts.<p>
These cuts would include the layoff of 1,492 county employees, the closing of 18 public health clinics and the trimming of nearly half a billion dollars from the overall budget.<p>
Protesters also delivered more than 3,000 cards and hundreds of letters from patients and public school students, including from Beethoven Elementary, which is slated to lose its school clinic.<p>
In response, the county is stepping up violence against demonstrators.<p>
Last week, at Cook County Hospital, <i>Chicago Sun-Times </i>reporter Steve Patterson was brutally thrown to the ground and arrested by hospital security while covering a protest against the cuts by the Chicago Coalition for the Homeless. "I was told I was seen chanting with the protesters, that I didn't have an appointment and that I was trespassing," said Patterson.<p>
But Stroger's hard-line may be his own undoing if public outcry continues against the cuts. On February 14, 12 Cook County Commissioners held a joint press conference with members of NNOC, AFSCME, Service Employees International Union and the Chicago Federation of Labor to propose an alternative budget plan, which would save all health care services by increasing fees, cutting administrative costs and patronage.<p>
Stroger publicly promised to veto the commissioners' proposal if it passed as written, but he has also been forced to propose his own budget amendment that includes keeping open 12 clinics, including all three in the south suburbs.<p>
The alternative proposal is certainly a step forward, but we need to demand no union layoffs and no fee increases. The commissioners should "chop from the top" to save county services.<p>
