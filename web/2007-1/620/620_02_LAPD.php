<font face="Times New Roman, Times, serif" size="5"><b>Victim of LA's deadly gang in blue</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Kurt Krueger</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 23, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES police in riot gear numbering around 100 converged into the eastside Ramona Gardens housing project February 6--to disperse a makeshift carwash intended to raise funds for the funeral of their latest victim.<p>
Mauricio Cornejo was the latest person to die in the custody of the LAPD. He stopped breathing in a holding cell at the Hollenbeck police station--after, witnesses say, police beatings where he was arrested and at the station.<p>
According to the police, Cornejo was stopped for a broken taillight, and was initially cooperative, but then walked away from them. Cornejo allegedly got rid of a handgun, and then "turned and confronted" one of the officers who followed him.<p>
The officer says he ordered Cornejo to stop--then pulled out an expandable baton and struck him in the arm and leg. The beating continued as more officers arrived. Cornejo seemed "impervious" to the blows, the police say--although witnesses described Cornejo as pleading for help. "This was not pretty," Capt. William Fierro later summarized. "However, it was necessary."<p>
At least three witnesses paint a very different picture. Norma Picasso said she saw officers hitting Cornejo in the head and body after he had been handcuffed. Another witness, Yolanda Puente, saw police kick Cornejo in the head and ribs and hit him with batons. A cell phone video of the incident also shows police delivering the blows.<p>
A third woman said the assault continued in the holding cell at the police station. She told a reporter she saw officers drag Cornejo down a hallway, and while he was handcuffed, kick him at least twice. She said she could hear someone say, "He's dying."<p>
Police say they noticed that Cornejo was having trouble breathing and called paramedics, but it was too late. Cornejo was pronounced dead while in the holding cell.<p>
Spokespeople for the LAPD have been quick to point out Cornejo may have been a member of Big Hazard, a gang near Ramona Gardens that they claim has targeted their officers.<p>
But residents of Ramona Gardens--the city's oldest remaining housing project, made up of Second World War-era barracks-like masonry buildings--say that their neighborhood has long been neglected by the city, but not the LAPD. "Growing up here is as close as you're going to get to living in a police state," Jose Navarro, a college student who grew up in the project, told the <i>Los Angeles Times</i>.<p>
Residents say police will stop them for no reason and issue jaywalking tickets to teenagers heading home from school. One told the <i>Times</i> that an officer recently ticketed him for "looking like a gang member"--although he had never been in a gang and has never been arrested.<p>
Navarro, who says he was a close friend of Cornejo, told the <i>Times </i>that the police use the gang label to "dehumanize" young people in the neighborhood. "There's little difference," he said, "between me and the so-called gang members."<p>
For residents of Ramona Gardens, Cornejo's death is history repeating itself. In 1991, a sheriff's deputy shot and killed an unarmed gang member, leading to a tense standoff with residents. In 1992, two Black families were targeted with firebombings in a community that is otherwise almost entirely Latino. In 1996, an angry crowd confronted police after a gun battle left a suspected gang member dead and an officer wounded.<p>
No one should accept the LAPD's excuse of cracking down on gang violence as justification for another murder.<p>

