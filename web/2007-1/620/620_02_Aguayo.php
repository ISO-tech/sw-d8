<font face="Times New Roman, Times, serif" size="4">Agust&iacute;n Aguayo faces seven years in prison for resisting the war</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"I saw countless innocent lives cut short"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 23, 2007 | Pages 1 and 2</font><p>

<font face="Times New Roman, Times, serif" size="3">SPC. AGUST&Iacute;N Aguayo did everything by the book. Now he faces seven years in a military prison for his troubles. On March 6, the U.S. Army will begin court-martial proceedings against Agust&iacute;n, who is currently confined at his base in Germany.<p>
Agust&iacute;n joined the military in early 2003, but before he deployed to Iraq in February 2004, he applied for conscientious objector (CO) status--after his views evolved to the point where he no longer felt he could participate in the occupation of Iraq.<p>
While his request was being processed, Agust&iacute;n went with his unit to Tikrit, Iraq, carried out his duties as a medic, and went on patrols and performed guard duty--but without ever loading his weapon.<p>
The Army denied Agust&iacute;n's request for a CO discharge, and one appeal and two years later, the military notified Agust&iacute;n that his unit would deploy again to Iraq in August 2006. Agust&iacute;n again informed his commanding officers that taking part in the war effort would violate his principles.<p>

