<font face="Times New Roman, Times, serif" size="3">"In my last deployment, I witnessed how soldiers dehumanize the Iraqi people with words and actions," Agust&iacute;n wrote in his blog as he outlined the reasons he could not return to the war zone. "I saw countless innocent lives which were shortened due to the war. I still struggle with the senselessness of it all--Iraqi civilians losing their lives because they drove too close to a convoy or a checkpoint, soldiers being shot by mistake by their own buddies, misunderstandings (due to the language barrier) leading to death.<p>
"This is not acceptable to me. It makes no sense that to better the lives of these civilians, they must first endure great human loss."<p>
On September 1, Agust&iacute;n went absent without leave (AWOL) to avoid deploying. He turned himself in a day later to accept whatever sanctions the military might impose on him--a less-than-honorable discharge, or even a court-martial and possible prison term.<p>
But to Agust&iacute;n's horror, the Army informed him he would be sent to Iraq--in leg irons, if necessary. Agust&iacute;n decided he had no choice but to go AWOL again, and then turn himself in a few weeks later.<p>
In an interview, Agustin's wife Helga explained that as time passed, Agust&iacute;n's opposition to the U.S. war drive against Iraq only deepened.<p>
"One of the care packages sent to the soldiers was a book on the history of Iraq," said Helga. "He said that it really changed what he believed. I mean, he was a conscientious objector, he believed that killing was wrong, but after reading that book he realized that the war in Iraq has essentially been created for the personal gain of a few people.<p>
"What he told me was that for a few corporations, it's in their best interests to keep the chaos going in Iraq. And he just came to believe that killing is wrong, but this war is wrong, too, because it's all motivated by money."<p>
Antiwar activists in the U.S. are organizing forums and pickets to coincide with Agust&iacute;n's court-martial. In Germany, too, peace activists have gathered outside of Agust&iacute;n's military base to demand his release.<p>
Like other war resisters before him, Agust&iacute;n's act of conscience has helped to highlight the injustices of the U.S. occupation of Iraq--and serve as an example that other troops can and should organize to oppose this war.<p>
<i>Gillian Russom contributed to this report</i>.<p>

