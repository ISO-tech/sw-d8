<font face="Times New Roman, Times, serif" size="5"><b>Stop &amp; Shop out to bust union</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Wayne Standley</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 23, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">WORKERS IN five locals of the United Food and Commercial Workers (UFCW), representing almost 43,000 members, voted almost unanimously to authorize a strike at all Stop &amp; Shop supermarkets in Massachusetts, Connecticut and Rhode Island.<p>
At the last minute, despite the overwhelming rank-and-file enthusiasm for a strike, UCFW officials called off the work action and agreed to further talks with management. This strategy has not only weakened the union's position at the bargaining table, but has set the stage for management to step up its attack.<p>
Stop &amp; Shop's real plan is not negotiation, but busting the union, as the company has ordered pre-cut meat to sell if butchers are on the picket line. Top on the list of management's demands is that workers pay a larger share of a new health insurance plan.<p>
Already, part-time employees--almost 80 percent of the workforce--have to wait two years before they can even become eligible for health care coverage. So it wasn't surprising that a recent report revealed that Stop &amp; Shop in Massachusetts has the second-largest number of workers (after Wal-Mart) who rely on MassHealth (Medicaid) or the state's free care pool for basic health care.<p>
The company also wants to eliminate the traditional defined pension benefit plan for all newly hired workers and replace it with a 401(k) plan. And management wants to cut the number of sick days, reduce the premium paid for overtime, holiday and Sunday work, and eliminate the work rotation schedule for holidays and Sundays.<p>
One UFCW official described negotiations as only "getting uglier and uglier" after management announced it would turn seafood and floral departments into self-service units, transferring or eliminating workers there.<p>
Further, the company has hired a union-busting "labor consultant" and taken out full-page ads in local newspapers for scabs, offering $15 an hour for full-time and $11 for part-time help --almost double the usual starting pay for new workers.<p>
Stop &amp; Shop, along with Giant Food and Tops grocery stores, is owned by Dutch conglomerate Royal Ahold N.V. Over the past few years, the company's market share has declined with competition from non-union and low-wage food retailers like Wal-Mart and discount clubs.<p>
In April 2006, Ahold installed Jos&eacute; Alvarez, formerly executive vice-president of supply and logistics at Stop &amp; Shop, as CEO. His main task is to implement Ahold's new "Strategy for Profitable Growth" plan to "reduce operating costs...and increase efficiency." If labor costs aren't reduced enough to increase profits in a store, it will be closed.<p>
Given this all-out assault, the union officials' threats of a strike just to get management back to the bargaining table won't achieve much.<p>
Last year, Stop &amp; Shop made more than $700 million in profits. It can well afford to provide a living wage and benefits for all its employees. And Stop &amp; Shop workers have shown with their strike vote that they're willing to fight.<p>
