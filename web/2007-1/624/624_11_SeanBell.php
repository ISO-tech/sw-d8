<font face="Times New Roman, Times, serif" size="4">Grand jury indicts three cops who murdered Sean Bell</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Police are out of control"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Rosa Haire</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 23, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--A grand jury investigating the fatal police shooting of Sean Bell has indicted three of the five New York Police Department officers involved.<p>
Bell, an unarmed African American man, was killed on what was to be his wedding day in a barrage of 50 police bullets November 25 in Queens. Two of Bell's friends, Trent Benefield and Joseph Guzman, were seriously injured.<p>
The indictments, announced March 19, leveled second-degree manslaughter and second-degree reckless endangerment charges at Detective Michael Oliver, who shot at Bell's car 31 times, and Detective Gescard Isnora, who fired 11 times. Oliver was also charged with first-degree assault and second-degree endangerment, while Isnora was also charged with second-degree assault.<p>
Another officer, Detective Marc Cooper, 39, who fired four shots, was charged with a misdemeanor--reckless endangerment. Two other officers involved, Paul Headly and Michael Carey, who were not charged at all--yet they too fired a total of four shots between them.<p>
The shooting came after plainclothes officers followed the three men after leaving Bell's bachelor party at a nightclub in Jamaica, Queens. Police claimed they heard someone in the crowd say that the men had a gun and followed Bell and his friends to their car, where they subsequently opened fire. All three men in the car were unarmed.<p>
The grand jury heard testimony from over 60 witnesses, including Bell's friends and officers who were on scene. Two witnesses told the grand jury that the undercover officers never identified themselves as police.<p>
A surprise witness came forward after the jury had already begun deliberations and testified that he heard gunshots fired at the police. This is a claim not even the police made--and that ballistic evidence had disproved.<p>
Bell's murder ignited outrage and protest in New York City from communities who are fed up with the racial profiling by the NYPD and lack of justice for victims of police brutality. People's Justice: Community Control and Police Accountability, a coalition formed after Bell's death, called a rally March 17 that turned out 200 people.<p>
Many were angry that not all five officers involved were indicted.<p>
Nicholas Heyward, Sr., whose 13 year-old son Nicholas Jr. was murdered by New York City Housing Authority police while playing with a toy gun in a stairwell, spoke at the rally. "There's been so many countless murders," said Heyward, "but the police remain free. We're tired of the police constantly walking over us."<p>
The indictments by no means guarantee a conviction, as the acquittal for the four officers who killed Amadou Diallo in 1999 attests.<p>
Brooklyn City Councilman Charles Barron said at the rally, "As long as Ray Kelly allows killer cops to kill with impunity and without consequences, if the system won't fix itself, we're going to force them to pay the price ourselves. Anything short of five murder charges is a travesty of justice. The police are out of control, and the only way we're going to stop it is ourselves, by any means necessary."<p>
<i>Lee Wengraf and Patrick Collins contributed to this report</i>.<p>

