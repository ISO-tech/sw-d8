<font face="Times New Roman, Times, serif" size="5"><b>Salem witch trials for a new century</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 23, 2007 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">KHALID SHEIKH Mohammed has confessed. To seemingly every plot the U.S. government ever hinted he might have been involved in.<p>
The news that Mohammed--the supposed "number three" in the al-Qaeda network--confessed to being involved in more than 30 terrorist plots or activities was applauded by Bush administration officials as proof that the U.S. detention and interrogation system is working.<p>
The mainstream media accepted the claims about Mohammed--or "KSM," as the U.S. government insists on calling him--almost without question.<p>
According to a heavily edited transcript released by the Pentagon, Mohammed--one of 14 so-called high-value prisoners of the U.S. "war on terror" undergoing a Combat Status Review Tribunal (CSRT) at Guant&aacute;namo Bay, Cuba--accepted responsibility for planning the September 11 attacks, personally killing <i>Wall Street Journal </i>reporter Daniel Pearl in 2002, and plotting the murder of former presidents Jimmy Carter and Bill Clinton, and Pope John Paul II.<p>
But no one can be sure what Mohammed is actually guilty of--because he, like many other detainees, was subjected to torture at the hands of the U.S. and its allies.<p>
Mohammed was one of the "war on terror" detainees who faced "extraordinary rendition"--the practice of the CIA shipping prisoners to foreign countries where torture isn't banned.<p>
While held with U.S. approval for more than two years in Jordan, Mohammed was put through a range of tortures--including, CIA officials admit, "waterboarding," a mock execution in which a detainee is made to believe they will drown. Mohammed's two sons, then aged 7 and 9, were also taken into custody by the CIA for interrogation--and to pressure their father to talk.<p>
Thus, Mohammed's "confessions" have about as much reliability as those from women coerced into saying they were witches during the Salem witch trials of the 17th century.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">THE BUSH administration brought Mohammed and 13 other detainees to Guant&aacute;namo Bay last year in an effort to deflect criticism over the continued practice of rendition, "enhanced" interrogation tactics and the continued denial of detainees' right to a trial.<p>
As <i>Socialist Worker</i> went to press, it was announced that another "high-value" detainee, Walid bin Attash, confessed at his CSRT to having a role in the bombings of two American embassies in Africa in 1998 and the attack on the U.S.S. Cole in Yemen in 2000.<p>
But not only is any information gained from a detainee who was rendered or tortured suspect--the CSRT process itself is riddled with abuses.<p>
CSRTs are supposed to determine whether a detainee should be declared an "enemy combatant"--which means they can then be held indefinitely. But no defense lawyers are allowed in, and the media is barred from the proceedings. Prisoners aren't allowed to see the "classified" evidence against them.<p>
The heavily censored transcript from Mohammed's tribunal shows what a mockery of justice the CSRTs are--and the lengths to which the Bush administration will go to get what it wants.<p>
At one point in the transcript, the unnamed tribunal head notes that Mohammed had stated in the past that he was tortured. When asked if he made statements because of the torture. Mohammed responds, "I cannot remember now..."The rest of the sentence--and other remarks he made, including a statement he reportedly wrote about being tortured--was redacted by the military.<p>
However, the military did release a portion of the transcript in which Mohammed says his latest statements weren't made under duress or pressure--proof enough, according to the Bush administration, that his confession is true.<p>
"There is nothing quite like this...outside the Moscow show trials that Stalin mounted," commented the British <i>Guardian's </i>Andrew Brown, "and if we accept Khalid Sheikh Mohammed's confession, we owe Stalin's ghost a handsome apology."<p>

