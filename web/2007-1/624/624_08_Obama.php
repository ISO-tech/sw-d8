<font face="Times New Roman, Times, serif" size="5"><b>Obama's chilling threats against Iran</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 23, 2007 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">I WANT to thank <i>Socialist Worker</i> for its ongoing analysis of the Democratic presidential candidates, which unlike the mainstream media goes beyond their rhetoric to expose their real politics.<p>
One place where all the candidates seem to show their true colors is in front of the American Israeli Public Affairs Committee (AIPAC). Sen. Barack Obama's speech on March 2 was a prime example.<p>
While I knew Obama supported war with Iran, his threats reached a chilling new high in this speech. In addition to reiterating that all options--including military--must be on the table, he added this lovely bit of unsubstantiated propaganda: "Iranian nuclear weapons would destabilize the region and could set off a new arms race...<p>
"Other nations would feel great pressure to accommodate Iranian demands. Terrorist groups with Iran's backing would feel emboldened to act even more brazenly under an Iranian nuclear umbrella. And as the A.Q. Kahn network in Pakistan demonstrated, Iran could spread this technology around the world."<p>
But what disturbed me most was the story he told about his trip to the "Holy Land." While he claims he took a helicopter overhead and traveled extensively on the ground, he seems to have missed (or ignored) the very existence of the Occupied Territories and the Palestinian people.<p>
Instead, he waxes poetic over an Israeli village, where "the houses and streets looked like ones you might find in a suburb in America."<p>
Does he contrast this with the Palestinian families whose homes have been bulldozed to make way for the apartheid wall, or those living in refugee camps lacking basic necessities like food and medical care?<p>
No. Instead he describes another Israeli house that was hit by Hezbollah rockets. He goes on to condemn Hezbollah and recognize by name every Israeli soldier who was kidnapped during last summer's war. No mention, of course, of the thousands of innocent Lebanese killed by Israel, including children who continue to be exploded by Israeli-planted cluster bombs.<p>
Apparently, the great liberal Obama only "feels the pain" of the Israeli people. This is particularly revolting considering that Obama depended on the support of the Palestinian-American community during his early political career.<p>
Nonetheless, "Obama's about-face is not surprising," wrote Ali Abunimah of the Electronic Intifada. "He is merely doing what he thinks is necessary to get elected and he will continue doing it as long as it keeps him in power.<p>
"Only if enough people know what Obama and his competitors stand for, and organize to compel them to pay attention to their concerns can there be any hope of altering the disastrous course of U.S. policy in the Middle East."<br>
<b>Leela Yellesetty</b>, Seattle<p>

