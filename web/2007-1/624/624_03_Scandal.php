<font face="Times New Roman, Times, serif" size="4">What the uproar is really about</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Up to their necks in scandal</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 23, 2007 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THE WAVES of discontent about the Bush administration are crashing over some of the president's most loyal servants--and lapping at the doors of the White House.<p>
According to the estimation of almost every political commentator, Attorney General Alberto Gonzales will be following former Cabinet colleague Donald Rumsfeld into early retirement within the month--if not his old White House pal Scooter Libby into the federal pen. And if Gonzales goes down, the next one in line to fall is the man they call "Bush's brain"--White House political adviser Karl Rove.<p>
The immediate threat to George Bush's diminishing ranks of henchmen is one of the administration's more minor crimes--a political purge of the Justice Department's staff of federal prosecutors.<p>
But make no mistake. This new scandal is another sign that the Bush administration is no longer considered competent to run the country--and not just by the two-thirds of the population who regularly tell pollsters they think Bush is doing an awful job, but by the people who actually count in the corrupt system of American politics: the ruling elite who inhabit the corporate boardrooms and the corridors of power in Washington.<p>
After all, getting rid of federal prosecutors for political reasons isn't unheard of. Bill Clinton, for example, fired all 93 U.S. attorneys after taking office in 1993, and other presidents have done the same--though unlike Bush, none has sacked a group of them in the middle of their four-year terms.<p>
The administration's firing of eight U.S. attorneys stands out for the baldness of its political motives--but also for the arrogance and ineptitude it shows. Asked about the meaning of the scandal on ABC's <i>This Week </i>political talk show, conservative pundit George Will said bluntly, "It's about competence."<p>
Press expos&eacute;s about the firings shared front-page space with articles about congressional testimony from Valerie Plame, the CIA agent outed by the White House in a heavy-handed attempt to punish her husband, former Ambassador Joseph Wilson, for exposing one of the lies to justify the invasion of Iraq.<p>
Earlier in the month, the trial of Dick Cheney's ex-chief of staff, Lewis "Scooter" Libby, for leaking Plame's identity ended in a guilty verdict--but jurors revealed afterward that they saw Libby as taking the fall for Cheney, who ordered and ran the smear campaign.<p>
Meanwhile, Gonzales' Justice Department is also under fire for misusing so-called "national security letters"--essentially, administrative subpoenas allowing the FBI to obtain e-mails, telephone records and other information without approval from a judge.<p>
A Justice Department internal investigation concluded last week "that the FBI has used this provision to illegally force businesses to turn over customer data, then lied to Congress about it," National Lawyers Guild President Marjorie Cohn wrote on the CounterPunch Web site.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">THE ADMINISTRATION would never have faced this outcry following September 11 or the seemingly swift U.S. "victory" in Iraq four years ago. The uproar is a symptom of deeper dissatisfaction with the Bush administration that reaches to the top of U.S. society.<p>
The grievances against the White House now emerging in the newly critical mainstream media are about many issues--the shredding of civil liberties, the right's fanaticism about gays and lesbians, lack of action on global warming. And looming behind them all is the disastrous U.S. war on Iraq.<p>
The 2006 election that swept the Democrats back into the majority in both the House and Senate was an unambiguous referendum against the war--which even sections of the political establishment had come to see as a catastrophe for U.S. imperial interests.<p>
America's rulers showed their displeasure with Bush then by beginning to shift support behind the Democrats. Now, they are using other means to put pressure on the administration--and scandals are among the most effective.<p>
But like any scandal, the firing of the attorneys provides a window onto the corruption and hypocrisy of politics in Washington--where partisan interests trump supposed commitments to democracy and liberty, no matter which party is in control.<p>
<i>Socialist Worker </i>will be as happy as anyone to see Gonzales or Rove go down. But it will take more struggle, led from outside Washington, to do something about the far worse scandals--government policies of war, repression and austerity, imposed on U.S. society and around the world, each and every day.<p>

