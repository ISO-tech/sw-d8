<font face="Times New Roman, Times, serif" size="5"><b>ICE raids spark protests</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 23, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">IN THE wake of a number of raids and deportations targeting undocumented immigrants, activists are organizing to send a message that we support immigrant rights.<p>
--In <b>Boston</b>, there has been flurry of activity since a March 6 raid by Immigration and Customs Enforcement (ICE) at the Michael Bianco Inc. textile plant in New Bedford, Mass., which rounded up 361 immigrant workers, mainly Guatemalan women.<p>
On March 13, Maya K'iche, a Mayan community organization based in New Bedford, Mass., organized a forum at Trinity Church in Boston. An extremely diverse crowd of around 700 people came to hear testimonies from three of the detained workers. One asked, "Why is it that I came to this country because of civil war, supported by the U.S., only to work hard and get treated like an animal here?"<p>
The next day the May Day Coalition organized a rally outside of the ICE headquarters in Boston. More than 100 people showed up from around the city, including a large number of students.<p>
And on March 17, more than 600 turned out for a rally in New Bedford called by the Massachusetts Immigrant and Refugee Agency, The rally had to be moved inside a high school due to bad weather, and approximately 150 people had to stand in the lobby because the auditorium had reached capacity.<p>
An impromptu speak out was organized, where Alex, the husband of a detained worker, told the crowd that his 8-month-old baby was hospitalized after being separated from her nursing mother for five days. When a speaker called for amnesty and to open the borders, the crowd erupted in applause.<p>
There is another protest planned for the coming week.<p>
--In <b>Providence, R.I., </b>activists picketed outside the ICE office March 12, also in protest of the raids in nearby New Bedford. Protesters marched around the office's front entrance, chanting "&iexcl;Justicia, s&iacute;! &iexcl;La migra no!" (Justice yes! Immigration no!) and "Set them free, now!"<p>
The International Socialist Organization, English for Action, the Committee of Immigrants in Action and Nuevo Amanecer of St. Teresa's Church, the Student Labor Alliance and MEChA at Brown University, and members of the campaign to free Eduardo Mathew participated.<p>
--In <b>Hartford, Conn., </b>a spirited picket of 40 immigrant rights activists converged March 16 in front of the Department of Homeland Security headquarters to protest a raid by ICE the previous day on the city's Dorothy St., which resulted in an unknown number of arrests and deportations<p>
The rally was called by the Regional Coalition for Immigrant Rights, and included La Voz Latina of Trinity College, Hartford HOPE, People of Faith, Latinos Contra la Guerra, Unid&aacute;d Latina en Acci&oacute;n of New Haven, New Haven Coalition for Immigrant Rights, the CCSU Progressive Students Alliance, Socialist Action, and the International Socialist Organization.<p>
"This has been a terrifying experience for my family," Maximo, one of those arrested said. "We have never caused any trouble here. We are not criminals."<p>
<i>Rebecca Bor, Alden Eagle and Rebecca Lewis contributed to this report</i>.<p>

