<font face="Times New Roman, Times, serif" size="5"><b>Katrina victims on strike at Miss. shipyard</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 23, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">PASCAGOULA, Miss.--More than 7,000 workers are on strike at a defense contractor's shipyard on the Gulf Coast in a battle with reverberations that go back to the Hurricane Katrina disaster.<p>
Earlier this year, Northrop Grumman tried to push through a contract with no wage increases and increased health care costs--on workers who live in towns that still haven't recovered from the hurricane. Almost every one of the strikers lost a home or a car.<p>
Nick Mariakas, an electrician at the Ingalls shipyard, told <i>Labor Notes </i>magazine that some of the Grumman workers "are still in FEMA trailers--and FEMA's fixing to take those trailers away. We can't live off what they're trying to pay us."<p>
When the workers--members of International Brotherhood of Electrical Workers Local 733 and more than a dozen different craft unions--voted down the no-wage-hike contract by a 90 percent margin, the company came back with a slightly better deal.<p>
But though the new proposal included a wage increase of $2.50 over three years, the cost of living on the Gulf Coast has increased sharply as a result of Katrina. For instance, a gallon of milk now costs almost $5 in Pascagoula.<p>
Workers concluded that the meager wage hike would leave them the same or worse off after three years, especially after a 50 percent increase in their monthly health insurance premium was taken into account. The workers voted down the deal, again by a wide margin, and began their strike on March 8.<p>
There are three other Grumman shipyards nearby in Mississippi and Louisiana, where workers voted to accept the revised contract proposal. But the Ingalls facility is the biggest and oldest and will set the terms of the contract at the other yards.<p>
Gulf Coast towns like Pascagoula are still rebuilding from Katrina, but Northrop Grumman made big bucks off the disaster, thanks to their friends in Washington. The U.S. Navy provided the company with a $2.7 billion contract to repair damage at the Ingalls shipyard, and FEMA kicked in another $356 million.<p>
But while billions went into the repairs, workers say they still face hazardous conditions at the plant, working with toxic chemicals and in high heat that often causes fainting.<p>
To build support for the walkout, more than 2,000 strikers held a six-mile march on March 12, from the plant gates to the town. Workers at the other three Grumman shipyards are still on the job, but many have come to Pascagoula to walk the picket lines.<p>
Strikers say that other unionists and members of the community have also showed their support. "We've still got people coming in left and right," Bobby Hinger, a carpenter, told a local newspaper. "We've got the support. The community has stepped up to the plate to help with donations."<p>

