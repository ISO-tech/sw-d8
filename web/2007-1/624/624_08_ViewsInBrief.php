<a name="Org"></a><font face="Times New Roman, Times, serif" size="4"><b>Organization and authority</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I WANT to thank Alex Fu for his letter, which inspired me to re-read Frederick Engels' "On Authority" ("The real debate on anarchism," March 16). But I came to an entirely different conclusion than Fu.<p>
It seems to me that Engels' writing centers on the argument that there's nothing inherently bad or "evil," as he puts it, about authority, and he poses the important question, "Is it possible to have organization without authority?"<p>
Engels then provides various examples as to why the answer to this question is a resounding "no." From the operation of a cotton-spinning mill, to the railway, and "on board a ship on the high seas," authority is an absolute necessity.<p>
Taking the argument further, he explains not only that the Paris Commune was authoritarian, but it wasn't authoritarian enough, identifying this weakness as one of the reasons it was crushed so brutally and so quickly.<p>
Authority is relative. Socialists are, of course, against the political authority of the capitalist state. But we stand very proudly for the authority of the exploited and oppressed over the parasitic, cruel, capitalist minority. There's no reason to paper over this or to shy away from the word itself.<p>
To call oneself "anti-authoritarian," as many anarchists do, is absolutist and misses this point. What's more, it leads us to the misguided and self-destructive conclusion that the problem lies with authority itself, rather than the more dynamic understanding that the problem lies with who has the authority. This can have dreadful political implications in organizing.<p>
In order for those of us who want to defeat this barbaric system to be successful, we must be honest and clear about what we're doing and saying.<p>
So I disagree strongly with Fu's claim that "there is no contradiction in an anarchist arguing for revolution and being anti-authoritarian, for it isn't authoritarian to destroy the authority of a minority class." It is, indeed, authoritarian. And, from our point of view, it's also glorious.<br>
<b>Lichi D'Amelio</b>, New York City<p>
