<p><a name="Left"></a><font face="Times New Roman, Times, serif" size="4"><b>Left holding their bag</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AS IF enough people weren't languishing in jails in New York on charges of petty crimes or drugs, the New York Police Department has set out to lock even more up.<p>
Last March, the NYPD started "Operation Lucky Bag," in which police planted unattended bags in subway stations around the city, waited to see who would take them, and then immediately swooped in to arrest them. It was recently reported that a total of 220 people were arrested last year in the sting--and it's still continuing.<p>
What a ridiculous stunt, and one that does something that I frankly thought was impossible: making the NYPD look even more cruel and useless when it comes to protecting the rights of ordinary people.<p>
According to New York police, Operation Lucky Bag has cut crime on subways and caught many "career criminals." But that's just not true, according to the statistics. In fact, more than half of those arrested had no prior criminal records.<p>
That includes Aquarius Cheers and Kia Graves, who recently went out to get diapers for their daughter and ended up getting busted instead. According to NY1 News, while waiting for a train at 59th Street, Cheers saw an unattended Verizon bag and looked inside. "I saw there was some electronics. Next thing I know, the train is coming," he said.<p>
The couple picked up the bag and rushed to make the train. "I was like 'just bring the bag,' not thinking twice about it," said Graves. "I was thinking, 'Oh we could find a receipt in there,' and possibly go back to the phone company Verizon." Instead, a team of undercover officers grabbed Cheers and charged him with petty larceny.<p>
What's especially sickening about Operation Lucky Bag is that police are basically playing a game of moral "chicken" with unsuspecting passers-by--betting (and hoping) that they can tempt people into committing a "crime." It's a perfect example of our cynical criminal justice system, and the bizarre things that constitute a crime in the society we live in.<p>
I, frankly, have some sympathy for a lot of people who may be desperate enough to "steal" an unattended bag. Maybe that person was recently laid off. Maybe they can't afford their exorbitant rent (like an increasing number of New Yorkers). Maybe they have kids they need to feed and clothe, and are wondering how to do it.<p>
And just how do the police distinguish between someone who picks a bag up to "steal" it, and someone who picks a bag up in order to see if they can return it to an owner--like Cheers and Graves? It's clear: they don't make any room for such ideas, despite the fact that even the city's own personal property law gives people 10 days to turn in found property worth more than $20.<p>
As the <i>New York Times</i> commented in a recent editorial, "It is remarkable how many people in this city are willing to track down the owners of lost cellphones, wallets or bags. Arresting good Samaritans is bad enough, but encouraging them not to help in the future through this kind of overly aggressive policing is a downright shame. The best thing to do with this misbegotten program would be to end it."<br>
<b>Lynn Scarff</b>, from the Internet<p>

