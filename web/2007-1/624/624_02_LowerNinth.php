<font face="Times New Roman, Times, serif" size="3">More than a year and a half after Hurricane Katrina, neighborhoods like the Lower Ninth Ward remain devastated and without social services. In the city as a whole, public mental health services are almost nonexistent.<p>
Burton, said friend and neighbor Louis Keller Jr. in a statement issued by the Common Ground Collective, "was a man suffering from an untreated mental illness and had been actively seeking assistance, but with the lack of mental health resources available in the city, he was left with no help."<p>
According to city officials, there isn't enough money to reopen Charity Hospital, the main public hospital in the city. But there is enough money to keep 300 National Guard members patrolling New Orleans since June 2006.<p>

