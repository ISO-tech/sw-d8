<font face="Times New Roman, Times, serif" size="5"><b>Mentally ill man shot dead in New Orleans</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Gimena Gordillo</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 23, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">A MENTALLY ill man was shot and killed by Louisiana National Guard military police in his home in New Orleans' Katrina-ravaged Lower Ninth Ward. Fifty-three-year-old Terry Burton was a frequent patient at mental hospitals for the last 30 years, according to his family.<p>
National Guard soldiers patrolling the Lower Ninth Ward in the early morning hours of March 8 saw Burton riding a bicycle and "behaving oddly." After an altercation in which he supposedly pulled a knife and threw a piece of broken glass at a soldier, Burton fled into the damaged home where he rode out the flooding from Hurricane Katrina a year and a half earlier.<p>
The National Guard soldiers, joined by New Orleans police, entered the house and confronted Burton. They say he pointed a gun at them--which turned out to be a "rusted, black metal BB gun"--and a National Guardsman shot him several times, killing him.<p>
Burton's family and community members called a vigil the following day, and then a protest at City Hall to demand a fair and thorough investigation. But the New Orleans Police Department immediately issued a statement that the shooting was "appropriate and lawful."<p>

