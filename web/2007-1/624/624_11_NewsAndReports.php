<p><a name="War"></a><font face="Times New Roman, Times, serif" size="4"><b>No to war and occupation</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--About 70 people attended an antiwar panel discussion March 15 at Holyrood Church in Washington Heights. The event was sponsored by the International Socialist Organization (ISO), Iraq Veterans Against the War (IVAW) and the Harlem Tenants Council.<p>
Panelist Fabian Bouthillette of the New York chapter of IVAW spent the summer of 2004 stationed on a ship near Iraq. "Our presence in Iraq is what is creating the violence--immediate withdrawal is both morally and strategically the best solution," he said.<p>
Community activist Rev. Luis Barrios of Iglesia San Romero de las Americas, added," If we're not in the business of raising consciousness, we're not going to mobilize people. If we don't mobilize people, we're not going to stop the war."<p>
Jen Roesch, of the ISO, was the third speaker. "We have an antiwar majority now for the first time. That is a change," she said. "Now we need to figure out how to build <i>between</i> protests."<p>
-- In <b>San Francisco, </b>about 60 students attended a panel discussion event with two members of IVAW at San Francisco State University March 14. The event was organized by Students Against War (a chapter of the Campus Antiwar Network) and was cosponsored by the General Union of Palestinian Students, Students for Social Change and the ISO.<p>
Christie Hubbard, an ex-military musician, witnessed the events of 9/11 at the Pentagon and worked exhuming bodies from the rubble of the World Trade Center, and left the military.<p>
Marine veteran Mike Ergo, who served two tours in Iraq, saw his friend killed during the siege of Falluja. "I've been to Iraq and some of my buddies are there, and I say we need to get out right now," he said.<p>
<i>John-Samuel MacKay and Michael Hoffman contributed to this report</i>.<p>

