<a name="Health"></a><font face="Times New Roman, Times, serif" size="4"><b>Stop the New York health care cuts</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jessica Rothenberg</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Tens of thousands of 1199SEIU health care workers and supporters marched and rallied in Manhattan to oppose Gov. Elliott Spitzer's budget proposal, which would cut $1.3 billion from state hospitals and nursing homes.<p>
Spitzer's plan would freeze payments to Medicaid providers and cut funding for public hospitals, teaching hospitals, nursing homes and home-care agencies--all measures that disproportionately affect the poor, uninsured, and elderly. These cuts are in addition to President Bush's proposed $1.2 billion cut from federal funds that go the state's health care budget.<p>
In recent weeks, Spitzer's spin doctors have focused on the budget's $10.6 million to expand medical coverage for uninsured children. But as one nurse noted, "That's literally nothing compared to what's being cut. And besides, if a kid can't get to the hospital because the local ones are closed, his insurance is pretty useless."<p>
The proposed cuts are especially hard to swallow given that two days of spending for the war on Iraq, which amounts to roughly $1.7 billion per day, would make them unnecessary.<p>
Some of the protesters expressed feeling betrayed by Spitzer, who is the first Democrat to be elected New York governor in more than a decade. "I truly didn't think he would do it," said a health aid from Nassau Public Hospital. "But he did--so we're just going to keep marching until he realizes what he's up against. We actually care about providing quality care to our patients, and we can't do that with all these cuts."<p>
Participants at the demonstration were optimistic and prepared to keep fighting. In the words of one protester, "We'll keep doing this until Spitzer either comes to his senses and gets his priorities straight or just has no choice but to back down."<p>
