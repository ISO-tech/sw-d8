<font face="Times New Roman, Times, serif" size="4">Washington state activists ready to put the war on trial</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Military resisters need your support</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 5, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Antiwar veteran CHANAN SUAREZ-DIAZ reports on developments in the struggle of military resisters in Washington state.</b><p>

THE 2007 New Year was ushered in with the depressing news that the 3,000th U.S. service member was killed in Iraq. The announcement was another reminder of the lives lost and ruined because of the lies conjured up by the Bush administration to carry out its war for oil and empire.<p>
Discontent with the war is on the increase among U.S. soldiers themselves, according a poll conducted for the Military Times group of newspapers for each branch of the armed forces.<p>
Barely one-third of the service members polled approved of George Bush's handling of the war, and a larger proportion said they disapproved. Only half of respondents thought success in Iraq was likely, and only 41 percent of the military said the U.S. should have gone to war in Iraq in the first place.<p>
Another reflection of the antiwar opposition among veterans and active-duty service members is the movement of military resisters. That movement will face an important battle here in Washington state when Army Lt. Ehren Watada faces a court-martial starting February 5.<p>
Watada is the first commissioned officer to refuse to be a part of the war on Iraq. He is being threatened with six years in prison for resisting, and the army is trying to max out his sentence to make an example of him.<p>
Antiwar activists are preparing for a series of activities before the court-martial.<p>
On January 21 and 22, Watada's supporters will participate in a "Citizen's Hearing on the Legality of U.S. Actions in Iraq"--designed to put the war on trial, rather than the brave men and women who resist it. Among the antiwar figures who will testify are former Pentagon analyst Daniel Ellsberg; Denis Halliday, who resigned in protest as United Nations coordinator of humanitarian aid in Iraq; and international law expert Richard Falk.<p>
Iraq Veterans Against the War is sending a bus of supporters to join with local activists in the organizing. Rallies are planned at the front gates of Fort Lewis on January 4, when a pre-trial hearing takes place, and February 5, when the trial starts.<p>
Another Army resister, Suzanne Swift, was released January 3 from confinement at Fort Lewis. One year ago, Swift refused orders to return to Iraq with her unit after enduring sexual assaults at the hands of commanders and fellow soldiers during her previous tour in Iraq.<p>
Swift said she both opposed the Iraq war as illegal and immoral, and feared further abuse if she redeployed. Her mother, Sara Rich, led a campaign to win Swift an honorable discharge and to raise awareness of the epidemic of sexual harassment in the military.<p>
But the Army kept up relentless pressure on Swift, and in December, she pled guilty to being absent without leave, under an agreement that includes a demotion and extension of her service to cover the time she was AWOL. The plea deal saves Swift from undergoing the trauma of a trial. But depending on where the Army assigns her now, she could be sent back to Iraq.<p>
Another Washington state resister, Sgt. Ricky Clousing, returned home last month from confinement in the brig on the East Coast. Clousing turned himself in at the August 2006 Veterans for Peace convention in Seattle, after going AWOL from his Army unit for over a year.<p>
Clousing served as an interrogator in Iraq and witnessed countless abuses against innocent Iraqis, including the murder of an Iraqi civilian by U.S. soldiers. He decided to leave his base at Fort Bragg after returning from his tour in Iraq. Clousing was given three months' confinement and a bad conduct discharge for pleading guilty.<p>
These resisters are heroes for standing up to an immoral and illegal war. They deserve our support.<p>

