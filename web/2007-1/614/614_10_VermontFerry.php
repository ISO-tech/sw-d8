<font face="Times New Roman, Times, serif" size="5"><b>Their campaign of fear and lies</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 5, 2007 | Page 10</font><p>

<font face="Times New Roman, Times, serif" size="3">A FEDERAL appeals court ruled last month that random searches of bags and car trunks on a commuter ferry system are constitutional. The ferry operates on Lake Champlain, between New York and Vermont, near the Canadian border.<p>
The Department of Homeland Security claimed that randomly selecting commuting passengers for searches is necessary to prevent terrorist attacks, and Judge Sonia Sotomayor agreed, saying the searches fall within the "closely guarded category of constitutionally permissible suspicionless searches."<p>
MIKE CASSIDY, an attorney at the Plattsburgh, N.Y., office of Prisoners' Legal Services of New York and a regular ferry passenger, filed the suit with attorney William Nelson and Vermont ACLU executive director Allen Gilbert.<p>
Cassidy talked to <i>Socialist Worker's </i>NANCY WELCH about the ruling and the impact it could have on Fourth Amendment protections for people nationwide.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>WHAT WAS your argument against Homeland Security's searches at the ferry docks?</b><p>

OUR CORE argument in the case was that, like the drug testing the Supreme Court rejected some years ago in <i>Chandler v. Miller </i>as purely symbolic to the war on drugs--which was then the "war" in vogue--this search regime is ineffectual and nothing more than symbolic. The searches are patently and inherently ineffective.<p>
The reality is that the whole search regime here is a joke. It's what I call theater security: a big charade that the government can actually protect us from terrorism in this way.<p>
The search program is part of the constant reminder that we're in danger, that we need to be fearful, that we need the government to carry out such programs to make us safe, that we are somehow safer because of these actions, and most importantly, that giving up fundamental civil liberties in the name of security is a necessary and acceptable price to pay.<p>

<b>SO DO these searches serve no purpose at all?</b><p>

THEY DO have one purpose, and it's also part of the security theater--to acclimate people to the idea that the national security state can do what it deems necessary to protect us from the people who hate us, while utterly failing to examine what is the major unasked question of our time: why people hate us so much, what are the root causes of terrorism and what should be done.<p>
In effect, instituting and upholding the fundamentally ineffectual searches is an utter failure to recognize that terrorism is simply one of the symptoms and consequences of our government's longstanding aggressive imperialist foreign policy.<p>
The government can't stop or prevent all acts of terrorism, certainly not without addressing root causes. Quite simply, it's impossible to protect us and deter this kind of terrorism, as our expert--a retired 20-year decorated Navy Seal who specialized in counter-terrorism--tried to explain to the court.<p>

<b>WHY SHOULD people be concerned about a ruling that seems to affect only the ferry system's 200,000 annual passengers?</b><p>

THE GOVERNMENT'S rationale and the court's upholding of it here allows for no limiting legal principle to prevent the complete gutting of our core Fourth Amendment protections from suspicionless searches.<p>
Under the rationale upheld by the court, the government could institute security checkpoints on any road, any bridge or any public place they like--all in the name of security and the so-called war on terror.<p>
In reality, what's preventing such searches from becoming even more widespread is budgetary concerns, not constitutional and fundamental civil liberties concerns and principles.<p>

<b>YOU'VE BEEN trying for two years to get the courts to halt these searches. Are you discouraged by the latest ruling?</b><p>

NATURALLY, I'M extremely disappointed by the court's decision. I think this is one of the more intellectually dishonest decisions I've seen, and I've seen my share of those. From purely a procedural standpoint, I should not have reasonably or legally lost this case at this stage.<p>
So yes, I find it very disheartening, frustrating and disappointing. Yet I am left with what motivated me to bring this challenge in the first place--to expose the charade and government campaign of fear and lies, and the results of this case have helped serve that purpose even in defeat.<p>
I simply could not and would not sit idly by while the absurdity and hypocrisy of the "war on terror" was hurled into my daily life. And my message now remains the same as well: so long as we allow the government policymakers to get away with perpetrating this kind of security theater, and shamefully dismantle our basic civil liberties in the process, they will continue to get away with all of the criminal foreign and domestic policies that are business as usual.<p>
Those policies are not only the real problem and source of terrorism, but are themselves murderous and barbarous acts of terrorism on a grand scale.<p>
Only when such criminal and terrorist acts by our own government are exposed for what they are and stopped--only through true democracy and social justice in both foreign and domestic policies--can we and the rest of the world ever hope to live in real peace and security.<p>

