<font face="Times New Roman, Times, serif" size="5"><b>"They killed him in the building we grew up in"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 5, 2007 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>KaShaun/"Son of Man"</b> is a music producer and artist, and childhood friend of the NYPD's first shooting victim since Sean Bell was killed--19-year-old Timur Person, who was gunned down in a building lobby on December 13. KaShaun talked to <i>Socialist Worker </i>about the latest shooting and the struggle against police violence.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>HOW DID you know Timur Person?</b><p>

HIS MOTHER raised me as a child. I was always over there. I didn't go to high school with Timur because he was younger. His brother and I went to school together. We were real tight, but Timur was always with us, too.<p>
He got a bullet in his heart--four shots in the chest. They killed him in the same building we grew up in. The police are saying that there was some kind of tussle, and they ran inside the building and grabbed him. The officer says he felt the gun press against his stomach, so he told the officers to shoot.<p>
I heard several stories. I'm trying to piece it together myself and find out the truth. When I first watched the news, they said that he drew a gun out on them. Then they changed the story that he had the gun pressed against the officer's stomach, and they said that the officer was on him and felt it. So there's a lot of stories coming out in the media.<p>
Timur was a good kid. He wasn't really about getting into trouble, the way that they say he was. Police are trained to subdue an individual. Their job is not to take a life, but to subdue someone and take them into custody.<p>
I'm not sure if he had a gun. If he did have a gun, there's a lot of stuff going on in this neighborhood. He's not out there shooting people. But there are a lot of people out here--gang members, drug dealers--so a lot of people out here feel like they have to protect themselves.<p>
If he had a gun, it was for protection, not to go out here running crazy. Like I said, he was a good kid. He wasn't about violence.<p>

<b>YOU WORKED with your uncle, the CEO of Putting God First Productions, in making music about police brutality. How did you get started?</b><p>

MY UNCLE Lee started a documentary about Kenneth "Mango" Mason, who was killed in our neighborhood in November. We went into the neighborhood with a camera to try to get the story. But when they see you with cameras, some people are suspicious.<p>
I'm a producer and an artist, so we produced a track in light of what Lee was doing. It was in the spirit of the times. After I did the song, Sean Bell got killed in Queens. Then another kid in the neighborhood got shot, but didn't die. That's when we thought, this is getting out of control.<p>
So we decided to do a video for the soundtrack--to give people something to look at and bring some kind of solution. That's how we got started with the single "The Police in the Community."<p>
I remember on December 13, I was walking the streets and just had a feeling like I was shot or something, and the next morning, I found out that another man died. Then they mentioned on TV that it was a 19 year old. I kept watching, and then they said that it was Timur Person, so I rewound the footage from the TV and watched it again.<p>
When we were working on the soundtrack, it was one thing, but when Timur got killed, it got <i>very </i>personal. I felt like I <i>have </i>to do something do more to change this world and this situation.<p>
What we're trying to do is bring some kind of solution. We don't want it to get to a point where there will be an all-out war between the police and the community. We want to figure out some way to bring peace. At the end of the day, we just want peace.<p>

<b>IN THE song, you say that the police are part of a "wicked system." Why do you say that?</b><p>

THE REASON I said that is that the police have authority, but they're abusing it--it's being used for wickedness.<p>
The mentality of the injustice system now is "shoot first, and shoot to kill." I can understand if you're shooting <i>back</i>, but that wasn't the case--you weren't shot at! If you're shot at, you have no choice, but these individuals aren't shooting at the police or even pointing a gun at them.<p>
During this entire season, since November, there's not a single situation where someone was pointing a gun or shooting at the police, but they're shooting at <i>us</i>!<p>

<b>WHAT ARE the police doing in your neighborhood?</b><p>

The minorities are targeted. They're getting stopped by police and harassed on a regular basis: "Let me see this, let me see that." But our rights say that they have to have some kind of probable cause. Officers in my building were asking me why I was there, in my own building, and asked to see my ID!<p>
They try to set people up for arrest. You're a target. They're like predators, and they say, "Let's go get this guy." They roll up on them and search them, but why are they doing that? They just picked them out of a crowd. A lot of times they didn't get a call or anything--they're just searching for <i>something</i> they can use.<p>
The police are supposed to be the protectors of a community, not trying to bring about a situation or <i>making</i> something occur.<p>

