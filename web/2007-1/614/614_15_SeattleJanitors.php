<font face="Times New Roman, Times, serif" size="5"><b>Seattle janitors strike for union recognition</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sam Bernstein</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 5, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--Two hundred janitors in the Seattle area are on strike for union recognition, a fair contract and respect on the job.<p>
Coming on the heels of a strike victory by Houston janitors, nonunion janitors working for Cascadian Building Maintenance throughout the Puget Sound region began walking out on December 4. Cascadian cleans the offices of companies such as Cingular, the <i>Seattle Post-Intelligencer </i>newspaper and the Bill &amp; Melinda Gates Foundation.<p>
Although Cascadian claims on its Web site to be "a progressive company with modern attitudes and proactive thinking," they pay janitors only $10 per hour and don't provide family health coverage. With the full-time living wage in King County calculated to be over $12 an hour for a single adult and $26 an hour for a worker with two children, the primarily Latino immigrant workforce currently lives in poverty.<p>
Workplace safety is another major issue. "The chemicals are very harsh," striking janitor Elvia Gonzalez told the Seattle Solidarity Network, and she's concerned that she and her coworkers will develop health problems if they aren't given adequate protection. Also, "they send us to clean very high places, and they don't give us ladders," she said.<p>
Cascadian management has been persistently hostile to janitors' efforts to organize with Service Employees International Union (SEIU) Local 6 and has used blatantly anti-union tactics, such as mandatory meetings in which management warns workers against the union.<p>
"They just don't want a union. They cannot accept it. I've been at Cascadian for two years, but I know a lot of workers who have been there for five, six years waiting to have a union," said Francisco Calero, who cleans a pharmaceutical building.<p>
In a strategy used by SEIU's Justice for Janitors campaign around the country, the union is seeking to put pressure on building and office owners who hire Cascadian. So far, workers and supporters have held noisy rallies at several Cascadian-cleaned buildings around Seattle.<p>
Unfortunately, however, the struggle has received virtually no media attention. This may be due in part to a less-common union strategy in which the strike started small with more workers gradually walking out each day, rather than all workers striking at once. This strategy is supposed to steadily increase the pressure on Cascadian, but it may have diminished the initial impact of the work stoppage instead.<p>
"We need the media to spread information about what's happening, and we need union members in other industries to support us," said Gonzalez. "We're willing to take all the consequences of this and to go until the end."<p>

