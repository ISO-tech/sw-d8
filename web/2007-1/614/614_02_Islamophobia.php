<font face="Times New Roman, Times, serif" size="5"><b>A bipartisan wave of Islamophobia</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 5, 2007 | Pages 1 and 2</font><p>

<font face="Times New Roman, Times, serif" size="3">MEMBERS OF Congress are stoking racism against Muslims and Arabs. And it isn't only right-wing Republicans--one of the most liberal Democrats in the Senate stands guilty of promoting Islamophobia.<p>
The Republicans have a new target for their bigotry in newly elected Minnesota Rep. Keith Ellison--the first Muslim ever elected to Congress and the first African American from the state.<p>
Rep. Virgil Goode (R-Va.) went after Ellison when Ellison--a criminal defense lawyer who converted to Islam in college--said that he planned to place his hand on the Koran during a congressional swearing-in ceremony in January.<p>
Ellison's decision was publicized by right-wing radio host Dennis Prager, who wrote in his Townhall.com blog, "Neither I nor tens of millions of other Americans will watch in silence as the Bible is replaced with another religious text for the first time since George Washington brought a Bible to his swearing-in."<p>
Goode then declared, "When I raise my hand to take the oath on Swearing-In Day, I will have the Bible in my other hand. I do not subscribe to using the Koran in any way."<p>
Goode, who also supports building a "security fence" on the border with Mexico and making English the official language, went on to condemn not just of Islam but all immigrants.<p>
"I fear that in the next century, we will have many more Muslims in the United States if we do not adopt the strict immigration policies that I believe are necessary to preserve the values and beliefs traditional to the United States of America and to prevent our resources from being swamped," Goode wrote in a letter sent to hundreds of Virginia voters.<p>
One detail that apparently escaped Goode is that Ellison isn't an immigrant. "I'm an African-American," said Ellison, who traces his American ancestors back to 1742. "I'm about as American as they come."<p>
"I'm a little incredulous about why anyone would care about what I'm going to swear on," Ellison said to a recent gathering in Detroit. "In fact, if I swore on a book that wasn't of my tradition...would you trust me?"<p>
Since his election in November, Ellison says he has received threatening phone calls and e-mails and death threats--the latest example of the frenzy of suspicion and hatred aimed at Muslims in the name of the "war on terror."<p>
But Republican wing-nuts aren't the only ones in on the witch-hunt. Last month, Sen. Barbara Boxer (D-Calif.) decided to take back an award she had given to an Islamic activist after conservatives complained that he belonged to an organization that supported so-called "terrorist activities."<p>
In December, Sacramento activist Basim Elkarra received a "certificate of accomplishment" from Boxer. But when Boxer discovered that Elkarra was a local official with the Council on American-Islamic Relations (CAIR)--the leading Muslim-American civil rights organization in the country--she took the award back.<p>
Boxer also told her staff to tell CAIR to remove a previous statement she had made endorsing CAIR from the group's Web site. In November, Boxer sent a letter to CAIR on its 10th anniversary, claiming "[a]s an advocate for justice and greater understanding, CAIR embodies what we should all strive to achieve."<p>
Now, Boxer says that she never saw that letter--nor knew about the award to Elkarra before it was sent out.<p>
CAIR officials say that Boxer is responding to claims by right-wing blogger Joe Kaufman--who regularly appears on the Web site of conservative witch-hunter David Horowitz--that CAIR members are connected to terrorism.<p>
"We feel that the same crowd who is pushing these smears against CAIR is the same crowd as the neocons that pushed us into the Iraq war," CAIR's Nihad Awad told <i>Newsweek</i>. "They are trying to smear the Muslim community, and they are trying to silence its voice. This takes us back to the McCarthy era."<p>
Horowitz was thrilled at the result. "I'm pleased that Boxer listened to us," Horowitz said. "The fact that Democrats are finally waking up is good."<p>
We have to oppose these scapegoaters--Republicans and Democrats alike.<p>

