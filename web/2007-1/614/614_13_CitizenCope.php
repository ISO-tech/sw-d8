<font face="Times New Roman, Times, serif" size="5"><b>Citizen Cope's political outrage</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Amy Muldoon</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 5, 2007 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Citizen Cope,</b></i> <i>Every Waking Moment, </i>Arista, 2006, $12.98.</font><p>

<font face="Times New Roman, Times, serif" size="3">A GENERATION of musicians from Beck to Limp Bizkit to Moby has fused musical styles with mixed success.<p>
Two things make Citizen Cope stand out within this non-genre: First, his songs don't just refer to older styles like funk, they seem to grow out of the same soil, and, second, there is not a molecule of irony in his music. No matter what genre Citizen Cope harnesses for the job--R&amp;B to reggae to folk to hip-hop--it rings true.<p>
Starting out in the 1990s as a DJ, Citizen Cope toured with the D.C. outfit Basehead, and the flavor of hip-hop lingers in many of the tracks in his new album, <i>Every Waking Moment</i>. But his lyrical style has more in common with Tom Waits than any MC, as he captures moods and characters in a few grumbled words.<p>
While most modern R&amp;B wallows in gratuitous sexual images and sounds like an inventory list at Saks Fifth Avenue, Cope puts out a radical vision that being in love can be fun and fulfilling. "When you get near, close enough to hear/My mind just settles/The thoughts just clear," he sings out celebrating his partner in "All Dressed Up."<p>
But his new album <i>Every Waking Moment</i> also has a wide streak political outrage, which it shares with his two earlier albums, <i>Citizen Cope</i> and <i>The Clarence Greenwood Recordings. </i>In "Friendly Fire," a slow, grinding track that declares "They shot him down/And he was innocent today/They run for cover/They got no answer/Why they left him for dead," the mood sums up a confused pain and edge of rage, which resonates in the wake of recent police murders.<p>
Cope's take on the presidency is slipped into "John Lennon": "The father's insane/The son is kinda strange...These crooked politicians/They're ordering the killing/Of innocent civilians/Build another prison/For the children to fill 'em."<p>
In "Left for Dead," Cope puts is best foot forward singing in classic blues style. "You been left for dead" he moans, "Sister you're better than that/Gonna beat 'em instead/Beat all the odds you got up ahead."<p>
The track is simultaneously personal, defiant, political and mournful. And he manages to wring more emotion out of the simplest chorus (the vowels a,e,i,o,u) than many singers get using the whole alphabet.<p>
In a recent on-line interview, Cope talked about being a white artist writing R&amp;B and speaking on injustice. "They think that as a white person you can't speak from your heart, that you can't say that the system is fucked up. I don't agree with that policy. I think everyone should say what they wanna say."<p>
Citizen Cope deserves to be encouraged to say what he wants to say, so buy <i>Every Waking Moment, </i>and check out his older albums for great tracks like "Bullet and a Target," "Penitentiary" and "Contact."<p>

