<p><a name="Ohio"></a><font face="Times New Roman, Times, serif" size="4"><b>Ohio janitors</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By David Hughley</b></font><p>

<font face="Times New Roman, Times, serif" size="3">COLUMBUS, Ohio--Latin American and Somali janitors spoke out about the discrimination and unsafe work conditions they face at Mid-American Cleaning Contractors' (MACC) at a community meeting on December 7. Greater Columbus Jobs with Justice hosted the event in commemoration of International Human Rights Day.<p>
Central Ohio is home to more than 100,000 immigrants and refugees. Community leaders estimate 45,000 to 60,000 Somali immigrants and refugees--many that fled war and violence--make Columbus the second largest Somali population in the U.S.<p>
Three Somali-Muslim women--Fordosa Mohamed, Sahro Ahmed and Sirad Jama--described violations of their rights to pray and constant racist discrimination. After being denied the opportunity for prayer, "nine workers filed a complaint at the headquarters, our supervisor said that he would fire us one by one, the next day four were fired right away and my hours were cut in half," said Sirad.<p>
Fordosa was told that her job would be made easier in exchange for clothes and money after she refused to stand on a chair and clean the ceiling when she was eight months pregnant.<p>
"Listening to Fordosa saddens me, because that same situation happened in our building," said Isabel Romero, a janitor at the Chase Corporate building. "I heard about a Somali girl that lost her baby...Our work is very hard...We clean everything they mess up. It's not fair."<p>
In June, the Occupational Safety and Health Administration investigated several "serious" health and safety violations at Chase--failure to offer hepatitis B vaccinations and proper training to handle blood-borne pathogens--after janitors reported having to clean up blood spills without proper cleaning supplies and rubber gloves.<p>
"One thing I tell you, every time you get your check they are stealing something, a couple of dollars or hours," said Isabel. "They ask us to arrive 15 minutes early and leave 15 minutes late, but they do not want to pay us," said Ivan Reyes. Ivan was recently fired for attempting to organize a union.<p>
Sahro was also fired for working to organize a union. "I wanted to improve my job, and for those I work with so that we all will have fair jobs," said Sahro. "This is a company that violates, not only Latinos, but all races," said Ivan.<p>
Abigail Nava, a janitor at Columbus Academy, explained that the company tries "to divide Somali, Latinos and Black Americans. They discriminate and try to get us to fight with each other at work, and the managers are fine with this."<p>

