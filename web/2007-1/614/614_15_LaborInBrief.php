<a name="LA"></a><font face="Times New Roman, Times, serif" size="4"><b>Los Angeles Unified School District</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs, United Teachers Los Angeles</b></font><p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES--A state law that would have given LA Mayor Antonio Villaraigosa a substantial measure of authority over the Los Angeles Unified School District (LAUSD) was struck down by a Superior Court judge last month.<p>
The <i>Los Angeles Times </i>described the ruling as a "stunning setback" for the mayor. By creating a major obstacle to Villaraigosa's efforts to put LA schools under mayoral control, the ruling is a huge victory for LA's teachers and the hundreds of thousands of predominantly Latino and Black working-class families who send their kids to LAUSD schools.<p>
Mayoral control has been a means of attacking teachers' union rights and silencing the voices of working-class communities in the education of their children in several U.S. cities, including New York and Chicago. The essence of the ruling asserts that mayoral control would violate the State Constitution by weakening the authority of democratically elected school boards and disenfranchising parents and community members.<p>
Villaraigosa's scheme, known locally as Assembly Bill (AB) 1381, was the result of a compromise struck last summer with leaders of United Teachers Los Angeles (UTLA).<p>
Seeking to neutralize teachers' opposition to his desire for full mayoral control and a vast expansion of charter schools in LAUSD, the mayor agreed to a complex power-sharing arrangement that would have given the mayor direct authority over roughly 10 percent of LAUSD schools. But AB 1381 was intended as a first step toward full mayoral control, and UTLA members rightly repudiated this rotten deal in a September referendum.<p>
Outrageously, UTLA President A.J. Duffy and other union leaders have continued to defend AB 1381 even though the referendum result changes UTLA's official policy to opposition to the mayor's scheme. Even after the judge's ruling, Joel Jordan, Duffy's chief political advisor, defended AB 1381 and "the possibility of [the mayor's schools] helping to bring about much-needed reform."<p>
For UTLA members, the most important lesson of this debacle is that the liberal darling Antonio Villaraigosa shares a fundamentally reactionary education agenda with the machine Democrat Richard Daley in Chicago and the Republican billionaire Michael Bloomberg in New York.<p>
When politicians of both mainstream parties talk about "school reform," they're really talking about scapegoating teachers, busting unions, disenfranchising working-class parents and ignoring the real problems of underfunded and overcrowded schools. The interests of teachers, students and parents have nothing in common with this reactionary agenda.<p>
