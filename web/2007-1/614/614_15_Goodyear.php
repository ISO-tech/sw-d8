<font face="Times New Roman, Times, serif" size="4">Concessions rankle many who spent 12 weeks on picket line</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Goodyear workers end strike</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Marshall Braun</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 5, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">SUN PRAIRIE, Wis.--After three months on strike, a majority of 12,000 workers at 12 plants across the country voted to accept major concessions in a new contract with tire manufacturing giant Goodyear.<p>
At the Sun Prairie plant, workers, members of United Steelworkers (USW) Local 904, voted 161-48 in favor of the three-year contract.<p>
Some workers, like 18-year employee Al Monroe, are satisfied with the settlement because workers "aren't losing health benefits or taking a drastic cut in pay."<p>
However, the deal cuts pay for new hires--and USW negotiators agreed to allow Goodyear to eliminate its medical plan for current workers and retirees and replace it with a Voluntary Employee Benefits Association (VEBA) without any long-term obligation to fund the plan.<p>
The company will pay $1 billion into the VEBA, up from $400 million from the company's previous offer. However, this agreement takes the burden off Goodyear to provide health care benefits, placing the responsibility on the workers to fund the trust into the future.<p>
The contract also calls for "market-based wage and benefit levels for all new hires," essentially a two-tier wage system which gives new employees a starting wage of $13 per hour. The introduction of unequal pay is a classic union-busting maneuver used by corporations for decades.<p>
Workers such as 30-year employee Barb Aliley are upset about the new system. "I feel bad for the new hires," she said. "They're not going to make the pay that we are, and the benefits probably are not as good."<p>
Goodyear also forced the union to accept the closure of a plant in Tyler, Tex., which employs more than 1,000 unionized workers. Union negotiators did win a one-year reprieve for Tyler workers to try to find other employment or accept a buyout.<p>
Workers at most of the non-tire producing auto parts plants are still fearing for their jobs, since Goodyear has been shopping these plants around for the past couple of years and nothing in the new contract guarantees their retention.<p>
Relations between strikers and management are bound to be stressed, since management tried to run production throughout the strike with the help of scabs. Some strikers have raw emotions, like one who tells of managers smiling and waving their paychecks at workers on the picket lines.<p>
 "It will take a while to forget about that," said Jodi Dushack, a 12-year worker. "Some of the management was okay to us, but some rubbed it in our faces," she said. "It's going to be hard because a lot of them you thought were your friends turned out to be--what word should I use?--vulgar."<p>
In Sun Prairie, workers like Jodi stayed strong despite only receiving $100 per week in coupons. Locally, only two unionized workers crossed the line in 12 weeks, despite the offer of old contract wages and benefits.<p>
Sun Prairie workers received a $100 coupon Christmas bonus, which allowed some to buy minimal gifts for their children. However, many couldn't afford to buy anything for friends or family during the holiday season, and most sorely missed three months of salary.<p>
With the Goodyear plant's future uncertain, many workers are wondering how long into the future the paychecks will keep coming.<p>

