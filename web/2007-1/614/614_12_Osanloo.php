<font face="Times New Roman, Times, serif" size="4">Mansour Osanloo's fight against state repression</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Union leader jailed in Iran</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 5, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">MANSOUR OSANLOO, the leader of Tehran and suburbs bus workers' union, was arrested again in November 2006. While walking on the street with other union activists, he was approached by some plainclothes secret agents and was taken to an unknown location.<p>
Mansour was recently released on $160,000 bail after spending eight months in solitary confinement in Evin prison. Following his arrest, Iranian political activists around the world managed to contact union leaders and progressive parties to participate in a campaign of protest.<p>
Union representatives from Canada, Venezuela and some European countries have expressed their concern and demanded the immediate release of the union leader. I hope the unions and progressive parties in U.S. follow their comrades in other countries and condemn the barbaric act of the Islamic government.<p>
What is Mansour's crime? Why is the government so afraid of him? He hasn't disrespected any religious figure--a crime which is punishable by death--or released the secrets of nuclear activities, or sold forbidden Marxist books, which is also a "no-no" in Islamic Iran.<p>
His only crime is to have organized the bus workers in a union--a union which was banned by the pro-imperialist Shah's regime, and banned again under the new government of the Islamic republic. He is trying to organize 40,000 workers who work in very harsh conditions and are poorly paid, far below the poverty line.<p>
Bus workers welcomed Mansour and his friends' invitation to reopen the union and joined. Experienced workers had seen that the Islamic shora is nothing but the regime's watchdog and tool to support the interests of management. The regime forced these kinds of shoras into workplaces after the 1979 revolution to take over workers' councils in many factories.<p>
Iranian workers, sick and tired of so many broken promises made by Rafsanjani, Khatami, Ahmadinejad and other authorities, organized their own movement. But the celebration didn't last long.<p>
In 2005, the secret police and other unidentified agents attacked their meetings, broke their banners and furniture, and, later on, entered their houses in the middle of the night and separated them from terrified family members, taking them off in their unmarked vehicles. Don't these scenes remind you of Nazi Germany, fascist Italy or occupied Palestine?<p>
The regime was forced to release Mansour last year following a worldwide campaign of support. Immediately after his release, Mansour resumed his activities in the union.<p>
His second arrest has raised a lot of questions. Now, as progressive political activists have been executed, forced into exile or silenced, Tehran bus workers are raising their hands and asking for solidarity from all unions and progressive parties around the world.<p>
The international response will bring a lot of joy and hope for Iranian workers. Free Mansour Osanloo now!<br>
<b>Sadegh Afroozi</b>, Los Angeles<p>

