<a name="Saddam"></a><font face="Times New Roman, Times, serif" size="4"><b>Saddam Hussein's execution</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SADDAM HUSSEIN was executed just before dawn on December 30. While I don't think socialists have any tears to shed for this man, the facts of his execution are absolutely despicable and speak to the brutality and terror of the U.S. occupation of Iraq.<p>
Saddam Hussein's "trial," as it was, took place under the auspices of several different incarnations of the U.S. puppet government of Iraq. Exactly what code of justice were they using? Was it the same code from one government to the next?<p>
Note that the U.S. had him put on trial in Iraq, and not at the International Court of Justice in the Hague--which does not have the death penalty as a possible punishment. All of Europe and many other countries around the world that have banned the death penalty must be shocked and awed at how "liberated" Iraq employs American-style frontier justice.<p>
If memory serves, Hussein's lawyers were often harassed, frequently denied access to their client, and I believe at least one was actually assassinated during the trial. Even Human Rights Watch--which decried the horrific acts of Hussein's regime--called the trial a sham and denounced it.<p>
With his final appeal turned down just five days prior, the Iraqi government acted to execute him minutes before the opening of the Islamic holiday of Eid, so as not to violate an Iraqi law that forbids execution of prisoners during religious holidays. And to top it off, they hanged him--how 19th century!<p>
And all of this is not even to mention the fact that the U.S.--the greatest purveyor of violence in the world--never had any right to overthrow the government, even of a corrupt dictator like Hussein. Or that while he was being tried for the murder of hundreds of Iraqis, the U.S. occupation was busy murdering hundreds of thousands. Or that U.S. forces had custody of him until just before the execution. Now, who was it that executed him?<p>
The crimes of Saddam Hussein pale in comparison to those of George W. Bush, who said Hussein "was executed after receiving a fair trial--the kind of justice he denied the victims of his brutal regime." Bush went on to say that "bringing Saddam Hussein to justice will not end the violence in Iraq, but it is an important milestone on Iraq's course to becoming a democracy that can govern, sustain, and defend itself, and be an ally in the war on terror."<p>
How many milestones have already been passed on the occupation's long road to nowhere? How can use of the death penalty make a society more democratic?<p>
And let's not forget that as governor of Texas, Bush executed more people than Saddam Hussein was sentenced to death for killing. Bush denied justice to the victims of his brutal regime just as much as the next tin-pot dictator. His hypocrisy is truly beyond human comprehension.<p>
Saddam Hussein's execution is yet another horrible scene within the panorama of barbarism that is the U.S. occupation of Iraq. This is why we need to say: Iraq for Iraqis, troops out now!<br>
<b>Brian Chidester</b>, Warren, R.I.<p>
