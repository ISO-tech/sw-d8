<p><a name="Stakes"></a><font face="Times New Roman, Times, serif" size="4"><b>The stakes in Oaxaca</b></font><p>

<font face="Times New Roman, Times, serif" size="3">TODD CHRETIEN'S response ("What's Next in Oaxaca?", December 8) to my letter ("Power in Oaxaca," November 17) misses my point and makes some revealing factual errors.<p>
Todd says Oaxaca can't be compared to the Paris Commune because only the teachers' union has struck, not more unions. But strikes played even less a role in the Commune.<p>
He says in Paris, "organized currents...fought to link the immediate struggles they waged to the goal of abolishing capitalism." Perhaps he is unaware that the Frente Popular Revolucionario, a group considering themselves revolutionary Marxists, have won a leading role within the Popular Assembly of the Peoples of Oaxaca (APPO). Their politics are flawed, but not more so than the Blanquist and Proudhonist sects that were the most influential anti-capitalists in Paris.<p>
We remember the Commune today not because it was a socialist revolution with a good chance of winning. We remember it because Marx, who thought its prospects were dim, both defended it and popularized the inspiring achievements of mass democracy in this, the first city run by the working class.<p>
My point was that, in a world where those ideals are considered impossible or outdated, if they are not completely forgotten, it is urgent to bring them back. And socialists who have had to inspire others with tales of soviets and communes from previous centuries should leap at the chance to once again popularize the details of a living example. I submit that we need to do more of this.<p>
Whether the APPO wins or loses in its demand that the governor step down, and whether Mexico's crisis deepens or subsides, workers' power in Oaxaca (for reasons Todd states) is likely to be fleeting, like the Commune, not infectious like Petrograd.<p>
But it isn't "wishful thinking" to call what has happened a (temporary, small) socialist revolution in the sense that the working class of Oaxaca has taken over the city and created the participatory structures to rule. And viewing the past through rose-colored glasses can make us miss startling new facts like these.<br>
<b>Avery Wear</b>, San Diego<p>

