<font face="Times New Roman, Times, serif" size="5"><b>Boiling anger at the NYPD</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 5, 2007 | Pages 8 and 9</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>JARED RODRIGUEZ reports from New York on the growing protests against police violence.</b><p>

TENS OF thousands of people took to the streets December 16 to protest the NYPD murder of Sean Bell, only hours before he was supposed to be married on November 25.<p>
The protest, which numbered as high as 40,000 according to some estimates, was the largest demonstration against police brutality since the sit-ins at City Hall and police headquarters to protest the murder of Amadou Diallo in 1999.<p>
The December 16 march, called by Rev. Al Sharpton, was the culmination of many local protests in Queens and elsewhere around the city.<p>
Sharpton had called for a silent demonstration, but that went unheeded in many parts of the march as protesters made their voices heard with loud chants. The most popular of the day was the crowd counting off the number of shots--50--fired at the car Bell was in.<p>
"This is a beautiful thing," said Harlem resident Maurice Williams. "This is a beginning--it's going to get bigger. It's not just about Sean Bell. It's bigger than that."<p>
More demonstrations are planned for this month--and as the new year got underway, Valerie Bell, Sean's mother, began a 50-day vigil outside the 103rd precinct, where the officers who shot her son are stationed. Bell is inviting supporters to join in the vigil.<p>
"They can't hide from what they have done," Bell told a reporter. "[We are] just showing people that justice will prevail, letting them know that we will go on, in Jesus' name, to do the right thing for our son and for anyone who has lost a child."<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">BELL WAS killed and two friends severely injured in the early morning hours of November 25 when New York police fired 50 bullets at their car.<p>
The three men were leaving a bachelor party in Queens, and police at first claimed that the car struck an undercover officer and one of the three was armed. But no weapon was ever found, and eyewitnesses contradicted the police account, saying a police van struck Bell's car, and the officers jumped out shooting.<p>
The other two shooting victims, Joseph Guzman and Trent Benefield, were taken to the hospital--where they were kept handcuffed to their beds, even though they were shot a total of 14 times and not charged with any crime.<p>
Departing from the habit of his predecessor, Rudolph Giuliani, of defending police no matter how bloody their crime, Mayor Michael Bloomberg was forced to admit that "it sounds to me like excessive force was used." He traveled to Queens to meet with Bell's family, and had his staff set up a meeting with Black elected officials and clergy.<p>
But outrage over Sean Bell's murder continued to seethe, especially in poor neighborhoods where the NYPD acts like an occupying army.<p>
The bitterness grew when police claimed they were hunting for a supposed "fourth man" who was near Bell's car and threatened officers with a gun. The cops carried out a series of raids on the homes of people who knew the victims.<p>
Anti-police brutality groups organized several smaller demonstrations following the shooting, but December 16 was the largest mobilization yet.<p>
Many community organizations and unions participated, including Service Employees International Union Local 32BJ, the 1199 health care workers union, Communication Workers of America, Transport Workers Union and the United Federation of Teachers (UFT).<p>
At a UFT delegate assembly before the protest, Sean Bell's teachers at John Adams High School addressed the delegates, urging them to organize a contingent. Many teachers joined in, wearing T-shirts that read, "Teach us, don't shoot us."<p>
"In New York City, people are always so close to that boiling point," said Katie Castellano, a teacher in a South Bronx school and UFT member. "There's our apartheid public schools--and gentrification, with people being kicked out of neighborhoods where they've lived for decades. "They're all connected to this. Sean Bell was the boiling point, and people are starting to draw the connections."<p>
Around the demonstration, impromptu debates took place about what strategies would take the movement forward. Many expressed disappointment that the march didn't end with rallies or speakers--instead, marshals ushered people home, though some individuals spontaneously began to give speeches and draw crowds.<p>
Because there was no rally, no clear next steps were put forward by march organizers. But local protests around New York City have continued, called by the New Black Panther Party, NAACP and other groups. Later in December, about 100 people crowded onto Wall Street in downtown Manhattan for a "Day of Outrage" protest. A demonstration at United Nations headquarters is being planned for January.<p>
Sean Bell's parents have called for police Commissioner Ray Kelly to be fired and an independent prosecutor to handle the case--demands that hold the potential of reigniting a movement against police brutality and for justice.<p>

