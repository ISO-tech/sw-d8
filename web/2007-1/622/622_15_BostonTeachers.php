<font face="Times New Roman, Times, serif" size="5"><b>Activists urge "no" vote on Boston teachers contract</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Gina Sartori, BTU</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 9, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">BOSTON--Rank-and-file activists are organizing to vote down a proposed concessionary contract being pushed by the leadership of the Boston Teachers Union (BTU).<p>
The tentative agreement came two weeks after BTU President Richard Stutman rammed through a motion at a union meeting postponing a strike vote for two weeks. Stutman caused an uproar by ruling that his motion had passed, even though voice and hand votes were too close to call.<p>
Just before the postponed strike vote was to take place on February 28, word spread through the media that the BTU had reached a tentative contract agreement with the School Committee. This demobilized union members, who had assumed that the contract struggle had ceased. Even so, more than 700 union members attended the February 28 meeting.<p>
Previously, a group of angry, rank-and-file BTU members met to form Boston Teachers for Action to argue for a strike and to pressure the leadership not to cave on key issues regarding class size, salary and healthcare.<p>
We arrived early to the February 28 meeting and placed informational leaflets on each chair. The Executive Board attempted to remove our leaflets, but backed down when we protested.<p>
At the start of the meeting, we saw that the vote to strike wasn't even on the agenda! Instead, the only motion up for a vote was one to affirm the BTU negotiating team's recommendation to accept the deal.<p>
The union failed to shrink the class size maximum of 31, which had been lowered in the previous contract. This issue was particularly painful because the leadership had scuttled the strike vote on February 14 primarily on the basis of their alleged gains on this issue.<p>
On the key issue of health care, the proposed contract dictates that member contributions would rise by 33.3 percent during the next three years. On average, teacher pay would rise a measly 3 percent a year. In effect, the new contract proposes a pay cut.<p>
Only members of the BTU's Executive Board and negotiating teams stood up to defend the contract. By contrast, teachers lined up to speak against the deal.<p>
Unfortunately, a motion to support the Executive Committee's deal passed. The vote was again close, however.<p>
Boston Teachers for Action advocates a "No" vote at the contract ratification meeting March 14. We're determined to continue organizing--no matter the outcome of the contract this year.<p>
