<font face="Times New Roman, Times, serif" size="5"><b>Indonesian thugs threaten left party</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 9, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">A LEFT-wing party in Indonesia was appealing for international solidarity after its regional conference was besieged by a right-wing militia as <i>Socialist Worker </i>went to press.<p>
According to a report from a member of the National Liberation Party of Unity, or PAPERNAS, that was circulated by Australian socialists, some 100 militia members broke into a party conference in East Java, leading to fighting with conference participants. Dita Sari, probably Indonesia's best-known radical leader and PAPERNAS's candidate in the next presidential election, was at the meeting and personally appealed for support.<p>
Conference organizers say they followed all the complicated procedures required to hold the conference, so it should have been protected by police. But in a repeat of previous confrontations, the police stood aside while the right-wingers menaced attendees.<p>
The Indonesian Anti-Communist Front (FAKI) turned up at PAPERNAS's founding conference in January and demanded that the meeting disperse. The congress kept to its schedule, electing a leadership and preparing for a year of "all-out campaigning"--but under constant threat of attack by FAKI members armed with knives and other weapons. The atmosphere of stress was so grave that one participant who was pregnant suffered a miscarriage.<p>

