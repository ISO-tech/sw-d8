<font face="Times New Roman, Times, serif" size="3">PAPERNAS unites a number of the left-wing forces involved in the uprising that toppled the dictator Gen. Suharto in 1998--chief among them, the People's Democratic Party, which Sari is chairperson of.<p>
This new effort at unity on the left has brought together activists in about 100 cities and towns around a program of nationalizing Indonesia's oil and gas industry, and cancellation of the foreign debt.<p>
Though the party has not yet met strict, undemocratic requirements to take part in elections, Indonesia's right wing--with its historic ties to the military--has clearly taken notice of a new threat to its dominance. When PAPERNAS meetings have not been menaced by FAKI thugs, attendees face the threat of police repression.<p>
As a statement from PAPERNAS leaders issued during the siege reads: "We call on all members and supporters of Papernas from the national level down to the grassroots not to retreat, but rather to continue political work with the people with even more spirit. This situation demonstrates that the political choice that we have taken has generated a reaction from those who are against the ideals of sovereignty for the people and the nation of Indonesia to become a reality.<p>
"We would also like to convey a request for solidarity form our sisters and brothers in Indonesia and overseas that also defend democracy and the same ideals, to lodge protest over this incident with the Indonesian government and the national police."<p>

