<a name="War"></a><font face="Times New Roman, Times, serif" size="4"><b>No to war and occupation</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ACROSS THE country, antiwar activists are turning out to build the antiwar movement and defend the right of soldiers to resist going to war.<p>
--At <b>San Francisco City College, </b>more than 200 students turned out February 28 for an antiwar teach-in and panel featuring Helga Aguayo, the wife of conscientious objector Agust&iacute;n Aguayo, who is facing a court-martial scheduled to begin March 6; Pablo Paredes, a conscientious objector from the Iraq war; and Susan Galleymore, of Courage to Resist.<p>
Aguayo talked about the horrors her husband Agust&iacute;n has witnessed while serving in Iraq--including the dehumanization of the Iraqi people by soldiers and the way soldiers themselves are treated. Galleymore discussed some of the lies that are advertised about joining the military, such as getting money for college, and the psychological and physical abuse that goes on in order to mentally break soldiers down.<p>
Later in the day, Pablo Paredes discussed how he was tricked by the military into joining in order to supposedly receive money for college.<p>
--At the <b>University of Vermont, </b>250 antiwar students and activists turned out March 2 to hear Cindy Sheehan and Vermont members of Iraq Veterans Against the War (IVAW) speak in support of a statewide town meeting resolution calling for impeachment George Bush and Dick Cheney.<p>
IVAW members Adrienne King, Matt Howard and Drew Cameron spoke in support of another town meeting resolution calling for bringing the troops home now by sharing their horrific experiences in Iraq and their experiences since coming home. "[Bush] hasn't sacrificed anything," said Cameron. "When he says sacrifice he means all the people in this room, we have to sacrifice while he and his buddies get rich."<p>
Sheehan, meanwhile, spoke about how impeachment isn't popular in Washington, even with the Democrats. "I remember seeing the shock on Rahm Emmanuel's face when we went to his press conference--as if he was saying, 'You're going to hold me accountable too?'"<p>
--At the <b>University of Texas </b>(UT) at Austin, military recruiters had their day ruined for the third year in a row as by a group of protesters came out to face them at "Explore UT Day"--which involves schools and parents bringing children to check out the university.<p>
A small coalition of members of the Campus Antiwar Movement to End the Occupations, Non-military Options for Youth and the International Socialist Organization came out to protest the recruiters, chanting, "Killing Iraqis is not a career; recruiters are not welcome here."<p>
 A 12-year-old girl from Killeen, Texas, the home of Fort Hood--which has sent so many soldiers to Iraq--said that "the war needs to stop. We really need to put more money in education."<p>
--In <b>Santa Cruz, Calif., </b>50 antiwar protesters crowded into City Council chambers February 27 to demonstrate their support for a resolution opposing a U.S. strike on Iran. The resolution argued that the deaths of more than 3,000 American soldiers in Iraq and "the waste of over $400 billion of American tax dollars might have been put to much better use." There are plans to hold an antiwar demonstration in downtown Santa Cruz on March 17.<p>
<i>Cindy Beringer, Owen Goodwin, Miguel Hernandez Salcedo and Steve Ramey contributed to this report</i>.<p>
