<p><a name="Imm"></a><font face="Times New Roman, Times, serif" size="4"><b>Immigrant rights</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IMMIGRANT RIGHTS activists have been organizing in the Bay Area to bring a halt to a wave of raids and deportations.<p>
-- In <b>San Francisco, </b>immigrant rights organizations initiated a week of demonstrations beginning February 26 in front Homeland Security to denounce raids, which have led to the deportation of 800 immigrants since October.<p>
These day-time rallies, as well as evening events throughout the Bay Area, were part of a week of action coordinated by the Bay Area Immigrant Rights Coalition and Deporten A La Migra to call for a moratorium on the raids, reject guest-worker programs and demand legalization for all.<p>
A Monday morning rally drew 100. By the end of the week, there was a march of 300 to the Federal Building where Speaker of the House Nancy Pelosi has her San Francisco office. "The government knows we need immigrants here. They repress us to keep people afraid, to keep us quiet so we won't organize or fight for our rights," said Susana Martinez, from La Raza Centro Legal.<p>
-- In <b>Oakland, Calif., </b>dozens of day laborers and immigrant rights activists turned out March 1 for a march and vigil in the Fruitvale district to oppose both the ICE raids and an increase in violence against day laborers.<p>
On February 18, a day laborer was slain while he waited for work. Day laborers are frequently scapegoated by anti-immigrant bigots and targeted for robbery, because they often get paid in cash. The rally called for unity, safety and justice in the community.<p>
<i>Kevin Chojczak, Brian Cruz, Jessica Kochick, Doniella Maher and Jessie Muldoon contributed to this report</i>.<p>

