<a name="Labor"></a><font face="Times New Roman, Times, serif" size="4"><b>Labor should unite with immigrants</b></font><p>

<font face="Times New Roman, Times, serif" size="3">VERMONT HAS a reputation for standing on the side of social justice; however, that spirit has yet to be extended to the state's estimated 2,000 dairy workers, most of whom are from Mexico and are undocumented.<p>
These men and women work long hours, starting as early as 3 a.m., for less than minimum-wage pay, and with scant access to basic health and educational services. They also live in extreme isolation and fear, because in Vermont, anti-immigrant profiling is on the rise.<p>
In 2006, more than 100 workers--men, women, and also their children--were deported, and in most cases, it appears to have been local and state law enforcement, Department of Motor Vehicle and Fish and Wildlife employees, or grocery store and bus line employees who made the first call to border patrol.<p>
In mid-February, several area activists and I attended a lunch with two dozen undocumented workers, who explained that because the climate has grown so hostile, they avoid even appearing in local grocery stores. Instead, when they have a chance to leave the farms to buy food, they drive extra miles to New York state.<p>
When we asked if it was border patrol they most feared each time they step off the farm, they replied no, it's municipal police, one particular state trooper, and also local vigilantes.<p>
Thanks to recent sympathetic press reports and public forums planned by activists, the plight of these workers who have long been laboring in the shadows is finally gaining public notice. Even Vermont's governor finally acknowledged that yes, the dairy farm operated by his in-laws depends on undocumented workers.<p>
But the governor still refuses to issue an order to state law enforcement, health officials and other service providers to do anything to improve conditions for farm workers and lessen their isolation and fear. More, the state's racist right has become emboldened, filling newspapers with vile anti-immigrant letters.<p>
Here's where we need Vermont's labor unions to get involved--not in support of the separate-and-unequal agricultural guest-worker program advocated by the governor, but for full civil and labor rights for all workers. As AFL-CIO President John Sweeney argued amid last spring's historic immigrant rights marches, "We must support immigrant workers because supporting all working people is the core of what it means to be a trade unionist."<p>
We are, as Sweeney argued last spring, stronger when we stand together, and especially when we call out the racism and xenophobia the bosses want to use to keep workers from uniting and fighting together.<p>
In a state where some 45 percent of jobs don't pay a living wage and in a state that, despite its reputation for social justice, has also in past decades put out the welcome mat for the KKK and proponents of eugenics, we've got a lot at stake in uniting to say: "Immigrants--not racists--are welcome in Vermont!"<br>
<b>Nancy Welch,</b> Immigrant Rights Vermont and University of Vermont United Academics-AFT/AAUP, Burlington, Vt.<p>
