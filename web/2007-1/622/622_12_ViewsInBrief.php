<p><a name="Scam"></a><font face="Times New Roman, Times, serif" size="4"><b>The "terror-free oil" scam</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THERE'S A new campaign out there to stoke hatred of Arabs and Muslims--and the mainstream media fell for it without a second thought.<p>
A pro-war group called Americans Against Hate has started what it calls the "Terror-Free Oil Initiative" (TFO)--opening several gas stations that sell gas that supposedly doesn't come from the Middle East, and are therefore "terror-free."<p>
As Ali Abunimah wrote on ElectronicIntifada.net, "The TFO logo features the Twin Towers and an outline of the Pentagon with the flight numbers of the aircraft that were crashed into them by hijackers on September 11, 2001. Other TFO propaganda features the image of Osama bin Laden with his face crossed out, reinforcing the message that the entire population of the Middle East should be viewed as indistinguishable from bin Laden."<p>
The "terror-free" oil scheme got a lot of press coverage, but most media outlets reported it as a quirky public-interest story, no different from any other publicity stunt. Actually, the figures behind the campaign are veterans of racist causes.<p>
Americans Against Hate's Web site is filled with diatribes about Islamists and "supporters of terrorism"--the two things being seen as synonymous. The group's founder and chair Joe Kaufman--and also the chief spokesperson for the "terror-free" gas stations--was one of the people yelling the loudest about Keith Ellison, the first Muslim to serve in Congress, earlier this year.<p>
Also, Kaufman wrote the article in David Horowitz's <i>Front Page </i>magazine that accused Barbara Boxer of being the "senator for terror"--because she gave an award to a member of the Council on American-Islamic Relation, a mainstream group that Kaufman nevertheless denounces as a front for terrorists and one of "America's live-in enemies." Shamefully, Boxer caved to the hysteria and withdrew the award.<p>
The TFO's claim to sell oil products that don't originate in the Middle East is a fraud. Its stations get gas from Sinclair Oil, a U.S.-based company that admits it can't confirm the origin of its supply.<p>
But the TFO's real purpose has nothing to do with where the oil comes from. As Abunimah wrote, "The 'Terror-Free Oil' initiative was a successful, headline-grabbing stunt by a group of unabashed racists and demagogues."<br>
<b>Jessie Conrad</b>, from the Internet<p>

