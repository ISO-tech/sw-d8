<font face="Times New Roman, Times, serif" size="5"><b>Bloc Party's statement against inequality</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Alexander Billet</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 9, 2007 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Bloc Party,<b> <i><a href="http://www.amazon.com/gp/redirect.html?ie=UTF8&location=http%3A%2F%2Fwww.amazon.com%2FWeekend-City-Bloc-Party%2Fdp%2FB000M06K5C%3Fie%3DUTF8%26s%3Dmusic%26qid%3D1173245304%26sr%3D8-1&tag=socialistwork-20&linkCode=ur2&camp=1789&creative=9325" target="_blank">A Weekend in the City</a>, </b></i>Vice Records 2007.</font><p>

<font face="Times New Roman, Times, serif" size="3">BLOC PARTY has definitely made a huge mark on modern music. Their debut album <i>Silent Alarm </i>displayed an energy and emotional honesty that clearly set them apart from most of the too-cool-for-school attitude of most "indie-rock" bands.<p>
It was this that prompted <i>NME</i> to claim Bloc Party "the band of 2005, no contest. As vital as The Clash in '77." Big shoes to fill, and the London-based outfit didn't disappoint. But despite this comparison, Bloc Party noticeably shied away from political issues.<p>
They denied that their song "Helicopter" was about George W. Bush, and lead singer Kele Okereke criticized Green Day for jumping on the "anti-Bush bandwagon." But a lot has happened since <i>Silent Alarm</i>'s release.<p>
Just ask Okereke. Rather than focus on the group's music, the mainstream music press zoomed in on the singer's ethnicity; he's the son of Nigerian immigrants. Then, they hounded him about rumors of his bisexuality, which he confirmed after a few months.<p>
Bloc Party has let those experiences shape its new album, <i>A Weekend in the City</i>. In doing so, the band has released a shockingly deep and powerful record. A step forward from <i>Silent Alarm</i>, the band has expanded its already intricate and layered sound, combining softer melodies with their raw frenetic power--a perfect compliment to Okereke's lyrics, which dare to venture into often taboo subjects.<p>
The same range of emotions is expressed on this album--alienation, despair, hope, anger. But Okereke has put them in a frightening and confusing world: modern London. "East London is a vampire/it sucks the joy right out of me," sets the tone in the album's opening track "Song for Clay (Disappear Here)."<p>
Bloc Party's London is especially alienating for groups seldom represented in pop music today: gays and people of color. "I just feel that every non-white teenager will know what I'm talking about when I say that certain avenues in this country are closed to you," Okereke told the British <i>Guardian</i> newspaper. "Whenever I walk into a pub in London I feel frightened. There are certain activities that are still predominantly white."<p>
This is the world--one of inequality and bigotry--that Okereke has unflinchingly written into <i>Weekend</i>. The track "Where Is Home?" highlights this in a gut-wrenching way as it starts at the funeral of Christopher Alaneme, a Black youth stabbed to death last April whom Okereke knew well. In a country where ASBOs ("Anti-Social Behavior Orders") are used to scapegoat anyone with dark skin, this song hits hard.<p>
Another highlight of the album comes in the menacing "Hunting for Witches," directed at the racist backlash following the London bombings in July 2005. "[That song was] written when I was just observing the reactions of the mainstream press and I was just amazed by how easy it'd been to whip them up into a fury," remarks Okereke. "I guess the point about the song for me is that post-September 11, the media has really traded on fear and the use of fear in controlling people."<p>
But it would be wrong to portray <i>Weekend </i>as overwhelmingly bleak. The beautifully Cure-esque "I Still Remember" is a vivid portrayal of modern love, more explicitly one about a secret gay rendezvous.<p>
When so many "love songs" trade in on cheap clich&eacute;, this tune is especially refreshing: "You should have asked me for it/I would have been brave/You should have asked me for it/How could I say no?" Love does take bravery, and doubly so when it's forbidden.<p>
If one characteristic could sum up this diverse album, it would be the very thing that has set Bloc Party apart from the beginning--willingness to be honest and open. In an image-obsessed industry, this band has been bold enough to eschew pretension and speak their minds.<p>
It's a hard thing to wear your heart on your sleeve. Bloc Party wears theirs with style. And this has lead them to produce one of the most poignant records in a long time. This is a flat-out incredible album. If Bloc Party continues on this path, they will end up one of the most important bands of our time.<p>

