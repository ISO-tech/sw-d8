<p><a name="UC"></a><font face="Times New Roman, Times, serif" size="4"><b>University of California-Santa Cruz</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jessica Shakarian</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SANTA CRUZ, Calif.--More than 100 students and AFSCME union members attended a Wages Not War rally February 15 at the University of California-Santa Cruz (UCSC). The action combined an antiwar message with labor's "Where's the Love?" rally for the long overdue wages promised by University President Robert Dynes.<p>
This year, Students Against War (SAW) united with AFSCME to highlight the connection between the rising military spending and the attacks on the working class at home.<p>
The rally began at noon, with speakers from SAW and AFSCME leading protesters in a march to the UCSC chancellor's office. Several people made inspirational speeches about how money for workers' wages is instead going into the war in Iraq. They argued that organization is necessary for people to build solidarity to end the war abroad and fight for justice at home.<p>
This solidarity walkout was part of a larger partnership with activists at other UC campuses and colleges around the country.<p>

