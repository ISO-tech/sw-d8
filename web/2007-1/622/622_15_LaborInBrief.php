<p><a name="NYC"></a><font face="Times New Roman, Times, serif" size="4"><b>New York City teachers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By John Yanno, UFT</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Members of the 100,000-strong United Federation of Teachers (UFT) will get a chance to show their dissatisfaction and anger with their current union leadership's strategy in union-wide elections this month.<p>
Two opposition caucuses, Independent Community of Educators (ICE) and Teachers for a Just Contract (TJC), have joined forces to run a slate of candidates challenging UFT president Randi Weingarten and her Unity caucus that has controlled the union since 1962.<p>
A TJC leaflet asks teachers, "Are you better off than you were three years ago?" when Weingarten was re-elected as president. "The answer is no."<p>
Teachers pay more for prescription drugs and health care. The 2005 contract increased a teacher's workweek by 50 minutes in addition to the 100 extra minutes added to the workweek in the previous contract. Teachers have lost the right to grieve negative letters and observations in their files, and senior teachers lost much of their seniority rights.<p>
Meanwhile, teachers' pay increases lag behind inflation. Between 1999 and 2005, the cost of an average apartment in NYC increased by 50 percent while pay for teachers increased only 34 to 40 percent.<p>
How do Weingarten and the Unity caucus get away with bargaining away teachers' rights? Through scare tactics.<p>
In 2005, Weingarten told teachers that if they didn't accept the contract, there would be no pay raises until 2009 when current Mayor Michael Bloomberg's term expires. Weingarten also told members that the UFT was not prepared to strike.<p>
TJC urged the UFT to instead prepare for an effective strike and picketed the UFT Delegate's Assembly to urge its members to vote no on the disastrous contract.<p>
The ICE-TJC slate promises to democratize the UFT, cutting UFT officer and staff salaries to the same level rank-and-file members receive. Additionally, ICE-TJC will fight for lower class sizes and to increase the salaries for paraprofessionals and secretaries.<p>
While the ICE-TJC slate is up against the Unity caucus's vastly greater resources and access to the membership (including the large number of retirees who vote), the campaign can help build a militant rank-and-file movement.<p>
Kit Wainer, ICE-TJC presidential candidate, said that this election was to "inspire, organize, listen to and learn from" UFT members. "We aim to use the election to build a rank-and-file movement of members who understand that a union is not just a law firm or a dental plan," he said.<p>
