<a name="Cook"></a><font face="Times New Roman, Times, serif" size="4"><b>Cook County budget cuts</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jesse Sharkey</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--Despite a hard-fought campaign by unionized health workers and community activists, the Cook County board approved a budget that will cut 1,155 health care workers, close 13 school-based health clinics and eliminate such crucial programs as infectious disease control from the prisons.<p>
Cook County President Todd Stroger's office says the budget will eliminate 2,193 jobs countywide.<p>
The campaign against the cuts included pickets, rallies, and a card- and letter-writing campaign by school kids. The National Nurses Organizing Committee (NNOC), which represents some 2,000 Cook County registered nurses, called the cuts "heartless" and "immoral" and vowed a fight.<p>
The Cook County health system delivers virtually all the public health services in Chicago, from public emergency rooms that turn no one away to walk-in clinics where thousands have their only access to primary care doctors. "It's going to cause death to patients," said NNOC member Batu Shakari at a die-in organized in front of the commissioners' meeting March 1.<p>
As part of the protests, nurses lay on the ground and held paper tombstones while Dr. Vietta Johnson of Provident Hospital explained, "Care to women and children is in severe jeopardy, it's been eliminated."<p>
The Cook County budget survived a challenge from a coalition of commissioners who tried to lessen the cuts by raising some taxes and targeting patronage jobs. But ultimately these challenges could not overcome the Democratic Party machine--which made an alliance with Republican commissioners to pass the budget.<p>
Pushing the cuts was Todd Stroger, who inherited his office from his father, Democratic Party machine honcho John Stroger, who won his primary fight last year while incapacitated by a stroke. Stroger the elder then handed office to his son, who won the general election by painting his opponent as a "Bush-style Republican" who would slash services.<p>
But if Todd Stroger was the lesser evil, it sure will be hard to tell for the thousands of front-line county employees slashed by his budget and the poor and working class people whose access to health care will be sharply curtailed.<p>
The NNOC as well as the Rev. Jesse Jackson are threatening civil lawsuits to stop the cuts.<p>
