<font face="Times New Roman, Times, serif" size="5"><b>Colorado's scheme to use prison labor</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 9, 2007 | Pages 1 and 2</font><p>

<font face="Times New Roman, Times, serif" size="3">COLORADO IS considering a new way to ease the shortage of farm labor created by harsh new anti-immigrant laws. Use prisoners instead.<p>
The plan to use prison labor in Colorado's fields is a stark admission of how the profitability of the state's agricultural economy depends on low wages paid to migrant farmworkers.<p>
Prisoners who opt for picking melons and peppers will be paid 60 cents--<i>per day.</i> Farmers, however, aren't thrilled with the deal, because they'll likely end up shelling out more than they have to pay migrant farmworkers to pay the cost of prison guards who'll act as overseers.<p>
Last summer, the state's Democratic-controlled legislature passed 11 anti-immigrant measures that it hailed as the toughest in the country, allowing police to check the immigration status of suspects and denying most non-emergency state benefits to anyone unable to provide proof of citizenship.<p>
In September, a series of raids and roundups convinced many of the state's undocumented residents they had no choice but to flee.<p>
Many legal residents also joined the exodus. "Some of them have said, 'We think our paperwork is in order, but how about if it's not, and we get caught on a glitch,'" explained farmer Joe Pisciotta. "This prison labor is not a cure for the immigration problem--it's just a Band-Aid," said Pisciotta.<p>
The draconian anti-immigrant laws are also creating labor shortages in other parts of the economy--for example, building sites in the construction industry.<p>
According to Mark Krikorian of the anti-immigrant Center for Immigration Studies, the Colorado prisoner-as-farmworker program is "a sign that there are solutions other than importing foreign labor."<p>
Krikorian promotes himself as a defender of native-born workers, whose jobs are "threatened" by migrant workers. But the truth is that immigrant workers take jobs that native-born workers won't--providing a lift to the economy and to wages in general.<p>
"The labor market is not a market of identical people," said Giovanni Peri, an economics professor at the University of California-Davis and the author of a new report by the Public Policy Institute of California. "Workers have different skills, perform different tasks, have different experiences, fill different positions."<p>
Peri's study confirms similar findings by other researchers--that because migrant workers are "more likely to take positions that are more physically demanding and require less knowledge of English or of local business practices," native-born workers are "able to take more specialized jobs...translating into a 4 percent real wage increase for American workers in California between 1990-2004."<p>
Wages also improve when native-born and immigrant workers have the same rights and protections on the job--and stand together in solidarity against the corporations and agribusinesses that seek to drive down pay and conditions for all workers.<p>

