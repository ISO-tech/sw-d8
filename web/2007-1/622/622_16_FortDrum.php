<font face="Times New Roman, Times, serif" size="5"><b>Bringing antiwar voices to soldiers</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 9, 2007 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">IRAQ WAR veterans and activists are working together to hold a series of antiwar events near Fort Drum in upstate New York, with the hope of increasing resistance within the military.<p>
The events are built around Different Drummer Caf&eacute;, a GI coffeehouse started last fall by Citizen Soldier director Tod Ensign. The other chief sponsor is Iraq Veterans Against the War (IVAW).<p>
Fort Drum has become an important Army base in the last 10 years. Three brigades are stationed there--with a total of almost 14,000 troops--and they have been heavily deployed to both Iraq and Afghanistan. In March, a whole brigade returns from deployment to Iraq.<p>
The first event, on March 9, features Ret. Col. Ann Wright speaking on "Dissent in Democracy: How to Avoid the Next Avoidable War."<p>
Wright has a combined 29 years of experience in the Army and Army Reserve, serving primarily in special operations units. In 1987, she entered the foreign service, but resigned in 2003 in protest of the Bush administration's rush to war with Iraq. Since then, she has worked to ensure that the needs of the men and women of the Armed Forces aren't ignored by the Bush administration.<p>
Wright will speak in the afternoon at Jefferson Community College and later in the evening at Different Drummer.<p>
On March 16, IVAW members Drew Cameron, Matt Howard and Matt Hrutkay will speak about their experiences on deployment in Iraq and how soldiers can join the movement to bring the troops home.<p>
Hrutkay was stationed at Fort Drum until November 2006. Cameron and Howard spoke to the Vermont state legislature before it voted last month to call on Congress and Bush to immediately begin the withdrawal of troops from Iraq.<p>
"Withdrawal is what we as veterans want, it is what the people of this country want, and it is what the Iraqis want," said Howard. "You can't win a crime, you can only stop it."<p>

