<font face="Times New Roman, Times, serif" size="5"><b>Two-year sentence for throwing a cup</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 9, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">I FIRST read about Jessica Hall and the "McMissile" case on the eve of her sentencing on February 21. The story is a stark illustration of the forces bearing down upon the working class and poor in the U.S., in particular working class and poor people of color.<p>
The bare facts of the case lay out like a Hollywood-styled "road rage" scene: Hall was trying to stay behind her father's truck on a family trip from North Carolina to New York, in traffic that had slowed to a crawl on Interstate 95 in Virginia. She had her three young children in the backseat, and her sister had begun having early contractions in the front. A couple in another car cut her off once, and then a second time.<p>
Hall snapped, pulled up alongside the offending car and threw a large McDonald's cup filled with ice through the window. Despite the fact that no one was injured and there was no damage beyond a sticky dashboard, Hall was charged with "maliciously throwing a missile into an occupied vehicle," which is a felony charge in Virginia.<p>
After being instructed that "any physical object can be considered a missile," the jury returned with a guilty verdict and a sentence of two years in prison, which is the minimum sentence they could recommend.<p>
Two years. For throwing a cup, filled with ice and a little bit of soda.<p>
Taking a step back, it's clear that while the "McMissile" aspect of the situation is novel, Hall's situation is all too common. Her husband is serving his third tour of duty in Iraq. The family lives off his $30,000 annual salary and food stamps. Hall herself is unemployed, and was to start nursing school the day after she was sent to jail to await final sentencing.<p>
As <i>Socialist Worker</i> consistently reports, the conditions that most people in the U.S. live under are getting more and more difficult. Health care is in crisis, government poverty measures like the newly coined term "food insecurity" and consumer prices are ticking steadily upwards, while decent jobs continue to be lost amid a clamor of jingoism and racist scapegoating.<p>
Hall spent a month in jail waiting for the judge to formally impose the sentence. Perhaps because the ludicrous nature of the charge was so glaring, he suspended the sentence on the condition of her five years of probation, but with the felony charge still on her record.<p>
As if that wasn't enough, as she was preparing to leave the jail, a two-year-old warrant for Hall turned up for check fraud in Mississippi. Despite the fact that Hall explained that her purse had been stolen, and that there was a police report corroborating that on file, she was barred from leaving and threatened with extradition to Mississippi unless she paid the fines, which totaled $833. Her father overnighted a check, and Hall was finally released the next day.<p>
Hall was charged with a felony for the "McMissile" case for no reason other than that she is poor and Black. In a post-Katrina United States, where the absolute criminal brutality of American racism has been dragged out into the light of day, we should be outraged, but unfortunately, not surprised.<p>
Indictments of people of other races on this same charge loom on the horizon, almost certainly to serve as a justification for this case and provide cover for prosecutors trying to dodge charges of racism. However those cases play out, one thing that can be said for certain is that the entirety of Hall's experience, from soup to nuts, falls squarely within the range of what we've come to expect from the U.S. economy and the U.S. criminal justice system.<p>
In a world where young Black men are statistically more likely to be in prison than in college, this story fits right in.<br>
<b>Christine Desrosiers</b>, San Francisco<p>

