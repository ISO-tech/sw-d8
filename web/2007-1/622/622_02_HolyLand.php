<font face="Times New Roman, Times, serif" size="5"><b>How the FBI framed a Muslim charity</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 9, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">YET MORE evidence of the government's persecution of Arabs and Muslims since September 11 has come to light.<p>
More than five years ago, the Bush administration shut down the Dallas-based Holy Land Foundation for Relief and Development, the largest Muslim charity in the U.S. The Feds labeled Holy Land a "specially designated global terrorist" and charged seven former foundation officials, including six U.S. citizens, with funneling money to overseas charities controlled by the Palestinian group Hamas, which the U.S. also has designated a terrorist organization.<p>
Much of the government's case against Holy Land was based on FBI "summaries" of thousands of hours of classified wiretapped conversations--including one of a 1996 conversation involving charity officials.<p>
But in late February, lawyers for the foundation revealed that much of the government's "official summary" of that conversation appears to have been fabricated.<p>
Of particular note, the government summary claimed that Holy Land officials repeatedly made anti-Semitic comments--but the 13-page verbatim transcript reveal no such remarks.<p>
For example, in the summary, the government quoted Holy Land's former executive director Shukri Abu Baker telling two associates there was no need to worry about the foundation being unfairly targeted because U.S. courts were not under the control of the American Israel Public Affairs Committee or "its sponsor, the government of the demons of Israel." But these words appear nowhere in the recently declassified transcript.<p>
Abu Baker also supposedly railed against "the Jews of the world" and claimed that Jews have no allegiance to anything but "their pockets and to preserving the illegal Zionist state of Israel." That isn't in the transcript either.<p>
Attorneys for the charity have asked a U.S. district court judge to declassify thousands of hours of FBI wiretaps in order to let the real statements come to light.<p>
Currently, although the defense lawyers have government clearances that allow them to review the material, under the federal Classified Information Procedures Act, they are prohibited from sharing it with their clients--a situation that makes it impossible for those accused to defend themselves from the Bush administration's charges.<p>
"[N]ot only are the summaries so inaccurate and misleading as to be useless," defense lawyers said in a court filing, but the "author of the attached summary has cynically and maliciously attributed to the defendants racist invective and inculpatory remarks the defendants never uttered." As the attorneys conclude, "It is appalling that such summaries even exist, much less that the government represented that this is all our clients need to know in order to defend themselves."<p>

