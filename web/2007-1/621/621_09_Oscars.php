<font face="Times New Roman, Times, serif" size="5"><b>Said and unsaid at the Oscars</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Leia Petty</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 2, 2007 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">THE BOLDEST political statement during the 2007 Academy Awards came when Al Gore and Leonardo DiCaprio read a teleprompted statement noting that the Oscars went "green."<p>
The lovefest for Al Gore unfortunately continued throughout the evening with joking calls for Gore to run for president in 2008. His film on climate change, <i>An Inconvenient Truth, </i>also won best documentary.<p>
Global warming is unquestionably a serious global concern, yet with bombs over Baghdad, threats on Iran and antiwar protests rocking the country, it is hard to imagine that these issues could be entirely ignored.<p>
They were. And while <i>An Inconvenient Truth </i>helps bring global warming into the mainstream debate, a look at the actual Clinton-Gore environmental record--handing out permits for toxic waste incinerators, opening up Western forests to logging and giving tax breaks to oil companies to drill in the Gulf of Mexico--tells a much less heroic truth.<p>
The silence on the war was amplified in the face of the political character of many films awarded this year. <i>The Queen </i>and <i>The King of Scotland </i>brought awards for best actor Forest Whitaker and actress Helen Mirren.<p>

