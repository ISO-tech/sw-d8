<font face="Times New Roman, Times, serif" size="3">Both movies seek to provide fictional transparency to the ruling elite--whether in conversations between Queen Elizabeth and Tony Blair regarding Princess Diana's death or in the rise of the dictator Idi Amin of Uganda.<p>
But why make and recognize two films that go behind the sealed doors of such rulers this year? The private decisions made by corporations and politicians, while necessary to maintain rampant inequality, also force the public to question and demand answers from political administrations supposedly acting in their interest.<p>
Scandals and lies beg for political transparency, and, given the past year for the Bush administration, it's no wonder filmmakers felt confident to write and direct such films. Yet the liberal limitations of these films offer no real framework for understanding how individuals rise to, hold or maintain power. And while Whitaker certainly deserves an Oscar for his performance in <i>The King of Scotland, </i>we are led to believe through these films that dictators and power are negotiated through personality.<p>
While the power of personality inevitably dominates films, whether through well-developed characters or the celebrities behind them, it becomes much more dangerous when it spills over to politics. Such logic leads many to believe that Bush's personality (no doubt stupid and crazed) is responsible for the disaster in Iraq.<p>
The conclusion for a political establishment in crisis, aided by the conclusions of these two films, is that all we need is a new personality.<p>
Peter Morgan, screenwriter for both movies, confident to speak more boldly at the Golden Globes last month said: "What do we have to do to get our leaders to listen to us? What do we have to do to get them to change tack? In 1997, 2.2 million people went on the streets of London, sleeping rough, bringing the biggest city of London to a standstill so that a stubborn 70-year-old lady would fly from Aberdeen to London.<p>
"What are we going to have to do when it is really important? You have to believe public protest counts for something." It definitely counts for a lot more than Al Gore ever will.<p>
While these films explore the personality behind the power, other movies awarded this year portray an escape from the devastation and misery inflicted onto ordinary people by those in power, portrayed brilliantly in one of the most-awarded films of the evening: <i>Pan's Labyrinth.</i><p>
Uncompromising in its solidarity with guerrillas standing up to fascism in Spain years after the defeat of a heroic civil war, <i>Pan's Labyrinth </i>is marked by a stunning visual escape into a dark fantasy world, winning for best art direction, makeup and cinematography.<p>
While the Academy Awards is inevitably filled with the most disgusting aspects of a society run by the rich and famous, this year's winning films contain glimpses of the desires of ordinary people: more transparency for those who supposedly rule in our interest, an escape from the devastation around us and hope for a world with different priorities than those who rule this one.<p>
Unfortunately no presenter or winner spoke to the world's most immediate desire--an end to all Bush's wars in the Middle East.<p>

