<font face="Times New Roman, Times, serif" size="5"><b>Why is Italy's Prodi facing a crisis?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 2, 2007 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THE TURMOIL engulfing the government of Italian Prime Minister Romano Prodi is providing a window into the crisis of Europe's center-left parties on the issue of war and imperialism.<p>
Prodi abruptly offered his resignation February 21 after suffering an embarrassing defeat in the upper house of Italy's parliament over foreign policy issues. The center-left government he leads seemed about to fall. But Italian President Giorgio Napolitano asked Prodi to stay on, pending a new vote of confidence in the parliament.<p>
As <i>Socialist Worker </i>went to press, Prodi was scrambling to find enough support to stay in office--and avoid a new round of elections that could shift the balance of power in Italian politics back to the right wing, which ruled Italy until last year under media baron Silvio Berlusconi.<p>
Prodi's resignation offer was prompted when he turned a foreign policy vote in the Italian senate into a vote of confidence in the government. Two left-wing senators abstained, and the confidence vote failed.<p>
Two issues caused the defections--the continued presence of Italian soldiers in Afghanistan; and the continuation of plans to double the size of the U.S. military base in Vicenza, in northern Italy, which had first been approved by Berlusconi.<p>
A few days before the vote, an estimated 100,000 people marched in Vicenza to oppose expansion of the base. And according to one poll, 62 percent of Italians--and 73 percent of the government's supporters--want all Italian troops pulled out of Afghanistan.<p>
Prodi has seen his popularity drop sharply since his nine-party Union coalition government--which includes former Christian Democrats, Communists, the Party of Communist Refoundation (PRC) and the Greens--came to power after a razor-thin victory over Berlusconi.<p>
At the time, the victory appeared as less an endorsement of the Union coalition than a rejection of Berlusconi and the right--in particular, their support for the U.S. "war on terror" and the occupation of Iraq.<p>
But Prodi has continued to defend Italy's participation in the occupation of Afghanistan--which he claims is legitimate because it is sanctioned by NATO and the United Nations. Meanwhile, his neoliberal Finance Minister Tommaso Padoa-Schioppa is continuing policies to cut workers' living standards and social services.<p>
Nervous of losing power, much of Italy's left is lining up behind Prodi for the new confidence vote. But Prodi, for his part, is appealing for the votes of several largely ceremonial "senators for life." And he says if he wins the new confidence vote, he will immediately seek to push through cuts in the state pension system and an increase in the retirement age--both proposals pushed by Berlusconi.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">THIS LATEST turn in Italian politics has revealed not only Prodi's true colors, but the weakness of the PRC, the left wing of the former Communist Party.<p>
A few years ago, the PRC was seen as the chief example of a developing left-wing challenge to the political status quo in several European countries. But as participants in the Prodi government, the party has moved to the right, supporting Italy's intervention in Afghanistan, Lebanon and elsewhere.<p>
The PRC leadership wasted no time last week in denouncing Franco Turgliatto, one of the two senators who abstained from the foreign policy vote. Turgliatto, a member of the "Critical Left" current within the PRC, resigned after he was condemned by the party executive.<p>
But as a statement circulated by socialists in support of Turgliatto explains, "In parliaments, we need representation of the positions of a peace movement with 'no ifs and no buts': we need such a representation now on the threshold of the spring offensive in Afghanistan and against subordination to the foreign policy of the U.S. We need acts like this, even if they are complicated and difficult, in order to reduce the gap between established politics and society."<p>

