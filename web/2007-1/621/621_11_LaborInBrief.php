<a name="Boston"></a><font face="Times New Roman, Times, serif" size="4"><b>Boston teachers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Brian Kwoba</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BOSTON--A planned vote on a one-day strike by 2,000 members of the Boston Teachers Union (BTU) at a February 14 meeting was hijacked by union leaders, who rammed through a vote to postpone action instead.<p>
The BTU, which represents about 8,000 teachers and staff members in the Boston Public Schools, has been without a contract since August and is in negotiations with the school district over salaries, health care costs and class sizes, among other issues.<p>
At the start of the meeting, BTU President Richard Stutman announced that earlier that day the district had "caved on class size" and that negotiations were looking better for the union. Stutman then announced that there would instead be a substitute motion on the floor that would delay the strike vote for another two weeks and allow the negotiations to proceed.<p>
After some intense debate, with premeditated interventions from key members of the executive board, the motion was put to a vote. Despite the fact that hundreds of union members were crowded in the back of the hall with no seats, a vote was taken--first vocally, and then by show of hands.<p>
The vote was clearly too close to call, but the union president called the votes for his motion to be the majority, at which point more uproar ensued. Eventually, amid the bedlam, the meeting chair called a "point of order"--that in the estimation of the chair, Stutman's motion to postpone the strike vote for two weeks had passed, and therefore the meeting was over.<p>
Understandably, many rank-and-file BTU members left the meeting furious at the decision they had just been force-fed by the union leadership.<p>
The anger was fueled by other outrages as well. First, in the hour leading up to the meeting, it had already been reported in the media that the union was going to vote on a substitute motion, meaning that the union leadership went to the press before it consulted its own membership.<p>
Second, the school district took the union to court in January on the grounds that Massachusetts law prohibits public employees from striking, and for weeks has constantly referred to any strike action as "illegal."<p>
"My greatest worry is not about this vote or even this contract settlement," said BTU member Marc Seiden. "My greatest worry is that we live in a time where the rights of ordinary people are being slowly chipped away. Do you realize that part of this whole debacle includes a court ruling that our union is not even allowed to <i>discuss</i> a strike? In my mind, the only way to fight such a radical ruling is to break that law.<p>
"There are many examples in history that show us that 'illegal' is not synonymous with 'wrong,' and that in fact it is the civil disobedience of organized people who break the law for what they know is just who help us come to terms with what is actually right."<p>
