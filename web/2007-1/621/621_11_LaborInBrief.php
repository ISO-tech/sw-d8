<p><a name="Harley"></a><font face="Times New Roman, Times, serif" size="4"><b>Harley-Davidson</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Joe Cleffie</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WORKERS AT Harley-Davidson's York, Pa., plant voted to return to work after a three-week strike that ended February 22.<p>
More than 2,700 members of the International Association of Machinists (IAM) Local Lodge 175 went on strike to fight against a proposed raise in health insurance costs as well as a company attempt to impose a two-tier wage system in the plant that would have prevented new hires from ever reaching the wage levels of older workers.<p>
The settlement, which passed with more than 80 percent approval, gives workers a 12 percent pay raise over three years. Workers will not have to pay for health care premiums like the company originally wanted, but they will have increased deductibles and co-pays.<p>
New hires will start at lower rates but can advance to the maximum pay of current employees. The union also gave some ground on matching funds contributed by the company to the employee pension fund.<p>
The deal is better than a contract accepted by workers at Harley's Milwaukee engine plant late last year, and it is an "improvement over the proposal rejected by workers earlier this month," Tom Boger, business manager for IAM Local Lodge 175, told the Associated Press.<p>

