<font face="Times New Roman, Times, serif" size="5"><b>Killed by hunger in a world of plenty</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 2, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">EVERY MINUTE of every day, 13 children die around the world of hunger and malnutrition. That's the finding of the United Nations World Food Program (WFP). Its latest report shows that 18,000 children die each day--or 750 each hour--of malnutrition and its related diseases.<p>
According to WFP, 850 million people are hungry or malnourished around the world on any given day. That is one in six of the world's population--or more than the combined population of the U.S., Russia, Japan, Germany, Britain and France. Half of the world's hungry are children.<p>
"This is a shameful fact--a terrible indictment of the world in 2007," James Morris, outgoing WFP executive director, told the Associated Press.<p>
In an article on its Web site, the WFP focuses on countries that it calls "hunger's global hot spots"--where war and natural disaster are keeping food from getting to much of the population. The list includes Afghanistan, where tuberculosis is on the rise, with an estimated 72,000 new cases reported every year; and Guinea, where about half the population lives in poverty, despite abundant resources.<p>
The WFP reports that almost half of the Palestinian population in the West Bank and Gaza remains food insecure or is at risk of becoming food insecure--as a result of "economic suffocation" imposed by Israel. According to a WFP report scheduled to come out this month, 84 percent of people living in Gaza had to cut back on their living expenditures by the end of 2006.<p>
"The poorest families are now living a meager existence, totally reliant on assistance, with no electricity or heating, and eating food prepared with water from bad sources," said Arnold Vercken, WFP's country director in the Occupied Territories. "This is putting their long-term health at risk."<p>
These unlivable living conditions reflect a world turned inside out by war and occupation--where food, water and other basic necessities are used like bargaining chips.<p>
They also shine a light on a world economy where an enormous transfer of wealth--from the bottom to the top--is taking place.<p>
According to a study released in December 2006 by the World Institute for Development Economics Research, the richest 1 percent of adults owned 40 percent of global assets, and the richest 10 percent of adults had 85 percent of the world's total wealth, as of 2000. On the other side of that equation, half of the world adult population on the bottom owned just 1 percent of global wealth.<p>
The yawning gap between rich and poor is becoming more and more pronounced, not just from country to country, but within countries as well.<p>
In the world's wealthiest country, the United States, nearly 16 million people are living in deep or severe poverty, according to an analysis of 2005 census figures by the McClatchy Newspapers. That's more than the total population of New York, Los Angeles, Chicago and San Francisco.<p>
What's so striking about this study is how many people had fallen into even deeper poverty than before. Today, 43 percent of the 37 million poor people in the U.S. have plunged into deep poverty--the highest rate since at least 1975.<p>
Of the 31 countries in the world considered to be economically developed, the U.S. has had the highest or near-highest poverty rates for children, individual adults and families over the last two decades.<p>
Despite this, the U.S. government has devoted precious few of its vast resources to alleviating poverty. According to the Luxembourg Income Study, a 23-year project that compared poverty and income data from 31 industrial nations, the U.S. devotes the smallest portion of its gross domestic product to federal anti-poverty programs of all the countries, except for Mexico and Russia.<p>
Meanwhile, the U.S. government plans to spend at least $650 billion this year on the military.<p>
If this starve-the-poor-to-feed-Corporate-America policy continues, the future is a grim one for working America. Some 58 percent of Americans between the ages of 20 and 75 will spend at least a year in poverty, according to research by the University of Wisconsin-Madison's Mark Rank reported in the McClatchy newspapers. "It would appear that for most Americans, the question is no longer if, but rather when, they will experience poverty," wrote Rank.<p>
It's a world of haves and have-nots--and the U.S. sets the standard.<p>

