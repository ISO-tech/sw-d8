<font face="Times New Roman, Times, serif" size="4">Abuse of prisoners in U.S. "war on terror"</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>License to torture</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 2, 2007 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">YEARS OF solitary confinement, sensory deprivation and overload, denial of contact with lawyers or family, forcible injections with unknown drugs. Most people would call this "torture." The Bush administration calls it standard operating procedure in the "war on terror."<p>
Some details of the abuses suffered by detainees at the U.S. prison camp in Guant&aacute;namo Bay, Cuba, have slowly emerged over the past several years as more and more former prisoners tell their stories. Less known, however, is the kind of torture that has taken place on U.S. soil--in the case of alleged al-Qaeda "dirty bomber" Jose Padilla.<p>
Padilla was arrested at O'Hare Airport in Chicago in May 2002 and later labeled an "enemy combatant" who was planning to detonate a radioactive bomb in the U.S. On this basis, Padilla has been imprisoned by the government for almost five years--much of it in solitary confinement in a Navy brig in South Carolina.<p>
According to his lawyers, he may have been driven insane by the treatment he's been subjected to.<p>
In a recent article in the <i>Nation</i> magazine, author and activist Naomi Klein described the cruel conditions Padilla has suffered. "He was kept in a 9-by-7-foot cell with no natural light, no clock and no calendar," Klein wrote. "Whenever Padilla left the cell, he was shackled and suited in heavy goggles and headphones.<p>
"Padilla was kept under these conditions for 1,307 days. He was forbidden contact with anyone but his interrogators, who punctured the extreme sensory deprivation with sensory overload, blasting him with harsh lights and pounding sounds. Padilla also says he was injected with a 'truth serum,' a substance his lawyers believe was LSD or PCP."<p>
The result: "Padilla has been so shattered that he lacks the ability to assist in his own defense," says Klein.<p>
One doctor who examined Padilla told a federal court that he rocks back and forth in his chair, has involuntary facial tics, breaks out in a sweat and verbally shuts down when asked to discuss this three and a half years in the naval brig. Another expert, Dr. Patricia Zapf, said that Padilla now shows a 98 percent probability of "brain injury" from his time in government custody.<p>
Government officials, on the other hand, claim that Padilla is faking illness to escape trial.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">FOR SOME critics of George Bush's war on Iraq--especially those within the political establishment--the treatment of prisoners of the "war on terror" is last on their list of concerns.<p>
But make no mistake: the abuse suffered by Padilla is no less a war crime than the massacres committed in Iraq and Afghanistan.<p>
And it is happening not just to one man or a handful of men, but to hundreds if not thousands of detainees around the globe. Similar prolonged isolation and abuse has been used against prisoners held at Guant&aacute;namo Bay, Iraq, Afghanistan and elsewhere. And this does not include the unknown numbers of people the U.S. has "rendered" to foreign governments like Saudi Arabia or Syria--to be subjected to torturous interrogations.<p>
If the Bush administration gets its way, even more detainees will be subjected to such dehumanizing treatment.<p>
A recent ruling by a federal appeals court declared that Guant&aacute;namo prisoners have no constitutional right to habeas corpus--the right to challenge their detentions in U.S. courts. Though the ruling is expected to be appealed to the U.S. Supreme Court, if it is allowed to stand, hundreds of detainees held around the globe would be left without any legal recourse to fight their imprisonment--leaving them at the mercy of their U.S. captors indefinitely.<p>
Despite rhetoric to the contrary, the Democrats helped pave the way for such abuses--by helping to pass the Military Commissions Act of 2006, which stripped detainees of their right to trial.<p>
Padilla's prosecution is critical for the Bush administration--a "slam-dunk" case it hopes will offset the series of failures documented in a recent audit by the Justice Department's inspector general, which concluded that the White House routinely overstated its successes.<p>
As Klein points out, it's not as if the government is unaware that the interrogation "techniques" it subjects Padilla and others to constitute torture. Even a revised Army field manual issued last year warns of the extreme psychological damage that prolonged sensory deprivation and other documented U.S. interrogation tactics can cause.<p>
"If these techniques drove Padilla insane," Klein concludes, "that means the U.S. government has been deliberately driving hundreds, possibly thousands, of prisoners insane around the world. What is on trial in Florida is not one man's mental state. It is the whole system of U.S. psychological torture."<p>

