<font face="Arial, Helvetica, sans-serif" size="2"><b>VIEWS AND VOICES</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Opponents of the war tell Rangel to cut funding</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Democrats need a backbone</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 2, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">APPROXIMATELY 150 people attended a meeting with Rep. Charles Rangel (D-N.Y.) at North Presbyterian Church in New York City on February 18.<p>
The meeting was hosted by Northern Manhattan Neighbors for Peace and Justice (a local chapter of United for Peace and Justice), Rev. Vernon Williams of New York City Baptist Peace Ministries, Uptown Youth for Peace and Justice, and other community groups. It was chaired by Judith LeBlanc, the national co-chair of United for Peace and Justice. Many of the attendees were from Military Families Speak Out.<p>
Rev. Williams spoke to the connection between the war in Iraq and the war at home: "The bombs that drop in Iraq explode in our streets. We poor people are the fuel for this war. When our young people lose a limb in Iraq, they can't find a VA hospital to treat them because they're all being closed."<p>
David Dubnau of Manhattan Neighbors for Peace and Justice was roundly applauded for saying, "There's only one way to end this war: nonbinding resolutions are not enough. Congress must vote to de-fund this war."<p>
Congressman Rangel oscillated between assertions of his opposition to the war and opposition to defunding the war because "we don't want to appear as though we're cutting the funding from under the troops."<p>
Rangel's preferred strategy is selective defunding of certain allocations, but not those required to withdraw the troops. The implication was that the selection process should be left to those in the know--professional politicians like himself.<p>
About 12 people were permitted to address Rangel with questions. While the opinions expressed varied, the overwhelming majority asked Rangel to defund the war in no uncertain terms.<p>
One member of Grannies for Peace linked very well the costs of the war with the social costs at home: "There were no weapons of mass destruction in Iraq, but there are WMD in our communities. There are more Black men in jail than in college. I call that genocide." Another speaker was applauded when she told Rangel, "It's time for the Democrats to develop a little backbone."<p>
Each time a questioner finished, Rangel rebutted with the mollifying homily, "I agree with you about this war, and I know you agree with me"--despite the fact that the speakers clearly disagreed with Rangel on a basic strategy to end the Iraq war.<p>
In his final wrap-up, Rangel admitted, "They [the Bush administration] had decided to invade Iraq long before 9/11"; "It's abundantly clear that we've killed more people in that part of the world than Saddam Hussein ever did"; and "You've got to keep up the pressure"--as if the antiwar movement is to remain eternally up on its hind legs begging favors of elected officials who feel no duty to oblige it in any way.<p>
Rangel also said, "As usual, the people are way ahead of their government." But he isn't powerless to reduce the distance between his position and that of his constituents.<p>
This meeting illustrated the growing frustration of people with the gap between the Democrats' rhetoric and their lack of action--and points out the need for a grassroots antiwar movement that can pressure politicians from outside the established party system.<br>
<b>Michele H. Showman, </b>New York City<p>

