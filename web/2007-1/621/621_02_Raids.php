<font face="Times New Roman, Times, serif" size="5"><b>Mothers taken from their children</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jason Farbman, Darrin Hoop and Jorge Torres</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 2, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">"THE ABDUCTION and detention were really ugly, but what's worse is worrying about your children while you're trapped in there." Those are the words of Erica Jimenez, one of three mothers of children under 18 months old who were swept up along with 48 other immigrant workers in a Valentine's Day raid by U.S. Immigration and Customs Enforcement (ICE) agents in Auburn, Wash., south of Seattle.<p>
The workers--who were employed by a contractor for UPS--were imprisoned for a week at a Tacoma detention center before an undisclosed number were deported. The others are expected to appear before a judge, with more deportations expected.<p>
The three mothers were kept away from their children--without any contact--for at least a week. Now, they are jobless, and faced with legal fees already amounting to $25,000.<p>
But the community response to this raid was immediate, and has been growing.<p>
An emergency protest was called just days after the raid, and took place outside the detention center gates. Though vastly outnumbered by armed guards in riot gear, the protesters made a ruckus that could be heard in the back of the building where the detainees were being kept. According to Erica, some detainees even started chanting along with the protesters outside.<p>
Last weekend, a fundraiser was held at St. Mary's in Seattle's Central District by El Comit&eacute; Pro-Amnist&iacute;a General y Justicia Social. Over the course of the two-hour event, $4,000 was raised to help offset the legal debts of the three women.<p>
Immigrant rights activists are publicizing this incident to help raise money for the families and put pressure on ICE to release all those still detained. El Comit&eacute; is circulating a letter and petition addressed to Sen. Patty Murray, calling on her to support a moratorium on raids. A picket and protest are planned in the near future outside the federal building in Seattle to draw attention to the government's abduction and detention of immigrants.<p>
The increasing frequency of ICE raids has highlighted the need to develop a more extensive emergency response network to mobilize activists to defend immigrants who are abducted in the future. This is an important step in the struggle to win full rights for immigrants.<p>
