<font face="Times New Roman, Times, serif" size="4">Anti-immigrant "hunt" on campus</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Protesting the bigots at NYU</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sarah Wolf</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 2, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">FOR IMMIGRANT workers facing the increasing threat of raids, detentions and deportation, being hunted by immigration agents is no game. But the College Republicans at New York University (NYU) tried to make it just that.<p>
The Republicans invited students to sign up to be mock "INS (sic) agents" and look for a student who had volunteered to play an undocumented immigrant--in an event called "Find the Illegal Immigrant."<p>
Fortunately, their "game" was ruined when a multiracial crowd of hundreds of students and community activists turned out to protest. Carrying signs that read "Racism is not a game," "Spot ignorance, not immigrants," and "No one is illegal," and chanting "Yellow, black, white, brown, immigrant labor built this town," demonstrators voiced both their anger about the racist hunt and their demand that immigrants be given equal rights in this country.<p>
At one point, protesters broke through lightly guarded police barricades to directly confront the Republicans, yelling "Racists out!" The protest ended with a march to the center of Washington Square Park, where organizer Dalia Yedidia declared, "This is a victory. They're scared now," and then called for a meeting to plan a pro-immigrant event at NYU in the future.<p>
Organizing to confront the College Republicans began as soon as activists from political and cultural groups on campus heard that the event was being planned on the "Facebook" Web site--and announced with cartoons picturing sombrero-wearing workers lining up to sneak into the U.S.<p>
A meeting to plan the protest drew more than 50 students, who spoke of their disgust at the racist nature of the event, which dehumanized undocumented immigrants by calling them "illegal" and implied they could be identified by appearance--i.e., skin color.<p>
Demonstrators had different opinions about why the Republicans were holding such a blatantly racist hunt. Mario Yedidia, a Columbia University student who helped organize a protest against Minuteman founder Jim Gilchrist last year, insisted that the event was "literally fascist" and was about intimidating "not just immigrant workers, but all workers."<p>
Meanwhile, a campus Democrat suggested, "They aren't trying to intimidate anyone, they're just stupid. This is a publicity stunt to promote their club." Many of the protesters last week disagreed.<p>
Moserrat Barrera, a freshman at NYU whose family was undocumented until a decade ago, connected the campus racism--and the protest against it--with the mass movement for immigrant rights that exploded last spring. She suggested that we need more protests like this one.<p>
"It's not a game," she told <i>Socialist Worker. </i>"I've seen raids, I've seen neighbors of mine deported. This is a subject that needs to be brought back after May 1. It went away in the media, but the struggle didn't go away."<p>

