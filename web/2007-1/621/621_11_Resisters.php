<font face="Times New Roman, Times, serif" size="3">In the months before Watada's mistrial, protest played a key role in drawing attention to his case. Antiwar activists rallied all over the country in support of Watada, with 1,000 people turning out to support him near the gates of Ft. Lewis during his trial.<p>
The antiwar climate influenced even the closed, rigged military court. In the days before Wilkerson's trial, antiwar activists organized support events, including a meeting the day before his court-martial hearing in Austin, Texas.<p>
Speakers included Kelly Dougherty, cofounder of Iraq Veterans Against the War, who Wilkerson met during the year and a half that he lived underground. Dougherty explained that soldiers are now making their second, third and even fourth redeployments to Iraq, and that a decision to refuse to fight is costly and potentially isolating to individuals like Wilkerson and Watada.<p>
Dougherty argued that the antiwar movement's active support for war resisters--politically, financially and socially--is essential "to build the confidence for others to speak out. Soldier resistance is key to the movement."<p>
Ann Wright, who resigned in 2003 from a diplomat position with the State Department in opposition to the Iraq war, noted an increase in the number of soldiers going AWOL. The Appeal for Redress has been signed already by 1,300 active-duty soldiers, noted Wright, and the list is growing.<p>
-- As it did in his first trial, activism will make the difference in the next stage of the <b>fight for Ehren Watada</b>. Supporters promise a renewed campaign to prevent him from being imprisoned, with the new slogan "Let Lt. Watada Resign!" His term of enlistment is nearly up.<p>
Watada's lawyer, Eric Seitz, blasted the military's decision to refile charges. He and many other legal experts believe that a retrial violates the Constitution's prohibition against "double jeopardy" (being tried twice for the same crime). "When it's not going well for you, you can't just call a mistrial and start all over." said Seitz.<p>
Whether the refiling of the charges stands up to the legal challenge, and whether Lt. Watada ever has to serve any time in prison is in doubt. How strong a movement develops in his defense will help determine the result.<p>
Lt. Watada has helped rebuild the antiwar movement by his courageous refusal to fight in Iraq. All antiwar activists owe him their strong support. Free Ehren Watada! Let him resign!<p>

