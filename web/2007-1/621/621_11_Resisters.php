<font face="Times New Roman, Times, serif" size="5"><b>We support war resisters</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Conor Reed and Steve Leigh</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 2, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">FT. HOOD, Texas--War resister Spc. Mark Wilkerson pleaded guilty to desertion on February 22 in a military court, with antiwar protesters showing their support.<p>
Wilkerson went AWOL in 2005 after the Army rejected the conscientious objector status he requested after being called up for a second deployment to Iraq. On Thursday, he was sentenced to military prison for seven months.<p>
For Wilkerson, who enlisted into the Army by age 17 and took part in the 2003 invasion in Iraq, going back in 2005 was not an option.<p>
In a statement titled "My Conscience is Clear," posted a day after his imprisonment, Wilkerson writes, "I could not deploy to a foreign land with a weapon in my hand, representing my government. I am not willing to kill, or be killed for my government. When I enlisted in the Army, I thought I would be able to, but after Iraq, my beliefs became such that I could no longer participate."<p>
The day after Wilkerson's trial, the Army hit back at Lt. Ehren Watada--the first U.S. officer to refuse orders to serve in Iraq whose first court-martial resulted in a mistrial last month--refiling charges against him in Ft. Lewis, Wash.<p>

