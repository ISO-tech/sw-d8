<font face="Times New Roman, Times, serif" size="3">It really came down to her needing a job and helping to protect her fellow soldiers, who she now considers her family, by going to Iraq with them.<p>
We talked about how it's horrible that she had to join the military, pick up a gun and go to fight a war in another country in order to get a job and education. When she mentioned it was better for her to go willingly rather than have a draft, we talked about how there is a draft right now--the poverty draft.<p>
I toldher the story of my brother, which is very much like her own. He joined the Navy shortly after high school because he felt he had no other choice. He had tried to go to engineering school, but couldn't handle working the full-time night shift while going to school full-time during the day.<p>
When his frustrations became too much to bear (actually, when the cops confiscated his car for having bad registration tags), the recruiters were right there to scoop him up. They had continued to call him on a weekly basis, even while he was trying to go to college.<p>
We also talked about the percentage of soldiers who are against the war, both at home and in Iraq, who want to come home now. Also, the Iraqis who overwhelmingly want the U.S. to leave. We talked about how she would not be alone in Iraq with her anti-war sentiments.<p>
I wanted to relate this story because I think my conversation with this young Marine is a window into what soldiers are thinking as they are leaving to Iraq for their first tour. Many are leaving being against the war.<br>
<b>Ragina Johnson,</b> San Francisco<p>

