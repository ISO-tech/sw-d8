<font face="Times New Roman, Times, serif" size="5"><b>A Marine who opposes the war</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 2, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">I WANTED to share with my experience talking with a young Marine who is going to be in Iraq shortly after her training this month.<p>
I was in the Mission District of San Francisco selling <i>Socialist Worker</i>. We were talking to people about Agust&iacute;n Aguayo, a conscientious objector who had been to Iraq and refused to load his gun. As I was asking people to sign a petition in support, a woman looked over her shoulder and said, "I'm a soldier, and I'm going to Iraq next week."<p>
I asked her what she thought about the war. At first, she was hesitant because she thought I was going to yell at her--she said she had been yelled at earlier in the day by antiwar protesters. I said, "No, I want to know what you think."<p>
She basically said that she's against the war and considers herself anti-war. She explained how being in the military was the only way for her to get a college education and have a job right now. Before she joined the Marines, she didn't have a way out of the poverty that she faces every day. She tried to go to college and couldn't afford it.<p>
She, of course, had mixed ideas. In one sentence, she'd start to say "We're fighting for America." Then she would stop herself and say, "I know its about oil," and "We're not there to help people or protect America."<p>

