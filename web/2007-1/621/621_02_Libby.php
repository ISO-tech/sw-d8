<font face="Times New Roman, Times, serif" size="4">Libby trial shows vice president ran smear campaign</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Will Cheney be held responsible?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 2, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">AS A jury considered the fate of former Bush administration official I. Lewis "Scooter" Libby, Dick Cheney has been revealed as the instigator of a smear campaign to protect the White House's war lies. But whatever the verdict on Libby, will Cheney ever be held responsible?<p>
Libby was charged with perjury and obstruction of justice by Patrick Fitzgerald, the special prosecutor investigating allegations that the White House leaked the identity of CIA agent Valerie Plame.<p>
Plame's outing was part of a scheme to discredit her husband, former Ambassador Joseph Wilson, who in 2003 went public to challenge one of the chief fabrications in the Bush administration's case to justify the invasion of Iraq.<p>
Wilson was sent by the CIA to Africa in 2002 to investigate claims that Saddam Hussein's government had tried to buy nuclear materials for a covert weapons program. Wilson concluded the "evidence" for the allegations were a crude forgery, and said as much in his report.<p>
But the administration continued to peddle the Niger uranium story in the run-up to the 2003 invasion--most famously, in Bush's January State of the Union address that year. In July, Wilson wrote an article for the <i>New York Times </i>debunking the story publicly.<p>
Cheney was furious. According to Libby's and other testimony, the vice president made notes for a response on his copy of the <i>Times</i>--and personally directed a campaign to discredit Wilson.<p>
Libby carried it out. Along with Bush's chief political adviser Karl Rove and White House press secretary Ari Fleischer, he set out to smear Wilson to the Washington press corps. To get the ball rolling, Cheney had George Bush declassify a portion of the National Intelligence Estimate on Iraq so it could be leaked, along with the identity of Wilson's wife, to soon-to-be-disgraced <i>New York Times </i>reporter Judith Miller.<p>
Before the trial began, Washington was filled with rumors that Libby's lawyers would put Cheney on the stand and finger him for ordering Libby to out Plame. In the event, neither Cheney nor Libby testified--and Libby's defense ultimately came down to his lawyer hysterically claiming that the jury shouldn't trust the parade of journalists who testified they heard Plame's identity from Libby.<p>
Nevertheless, Cheney's role came through loud and clear. Even the conservative special prosecutor Patrick Fitzgerald cast suspicion on Cheney in his closing statement. "There is a cloud over the vice president," Fitzgerald said. "He wrote on those columns. He had those meetings. He sent Libby off to the meeting with Judith Miller where Plame was discussed."<p>
As liberal columnist Sidney Blumenthal summarized, "In no uncertain terms, in his most public statement, Fitzgerald made clear that he believed that Cheney was the one behind the crime for which he was prosecuting Libby. It was Cheney who was the boss, Cheney who gave the orders, and Cheney to whom Libby was the loyal soldier, and it is Cheney for whom Libby is covering up."<p>
But there seems little likelihood that Cheney will be held responsible. Fitzgerald's investigation will probably end with Libby's prosecution.<p>
It will be the job of the antiwar movement to hold the Bush administration to account for its war lies--by mobilizing a struggle to end the disastrous occupation of Iraq and stop its new war drive against Iran.<p>

