<font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Bush administration keeps eye on the prize of a new Iraqi oil law</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Spilling more blood for oil</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 2, 2007 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">WHAT CAN possibly explain the Bush administration's stubborn insistence on a surge of 21,500 U.S. troops to Iraq?<p>
Nearly seven in 10 Americans oppose the plan. Support among the troops for an escalation is possibly even lower, considering that 72 percent said a year ago that they wanted to be home by the end of 2006. And though the U.S. media do their best to ignore it, the vast majority of Iraqis want the U.S. to leave.<p>
The U.S. escalation doesn't stop with the additional troops, either. Bush is also issuing new rules of engagement directing U.S. forces to open fire on Iranians found in Iraq. Two aircraft carriers and the battle groups assigned to escort them were sent to the Persian Gulf "to remind the Iranians that we can focus on them, too," according to a senior military official.<p>
Bush's escalation isn't a sign of complete insanity, however. The White House is faced with the question that confronts every occupying power challenged by a revolt of the occupied: Is it time to cut their losses and withdraw, or can they hope for an advantage through escalation, and even spreading the war.<p>
At this point, Bush and Co. are calculating that the stakes are too high to retreat--especially with the U.S. on the verge of achieving an important victory. Yes, victory--at least in the pursuit of one of its main goals when the U.S. went to war: not liberating Iraqi people, but liberating Iraqi oil.<p>
Control of Iraq's oil sector has been a constant preoccupation of U.S. officials. Now, proposed legislation before the Iraqi parliament would give U.S. oil companies virtual control over the second-largest proven reserves of oil in the world.<p>
As journalist Chris Floyd explained, "The new law offers the barreling buccaneers of the West a juicy set of production-sharing agreements that will maintain a fig leaf of Iraqi ownership of the nation's oil industry--while letting Bush's Big Oil buddies rake off up to 75 percent of all oil profits for an indefinite period up front, until they decide that their 'infrastructure investments' have been repaid."<p>
Only a handful of Iraq's parliament members have seen the draft of the law--because the broad outlines of the law weren't drawn up in Iraq, but in the U.S., by Virginia-based consulting firm BearingPoint, which has a lucrative U.S. government contract to "assist" in developing Iraq's financial and economic infrastructure.<p>
The prospect of controlling Iraqi oil--and, through military power, dominance of the oil-rich Persian Gulf--is the strongest argument that Bush and Dick Cheney have against their establishment critics. Of course, such a discussion can't take place publicly--which is why Bush justifies the escalation in Iraq in terms of the "war on terror" against "Islamic extremists."<p>
"Bush and his inner circle," Floyd concluded, "believe that a bigger dose of blood and iron in Iraq will produce a sufficient level of stability to allow the oil majors to cash in the PSA chips that more than 3,000 American soldiers have purchased for them with their lives."<p>
By seeking a confrontation with Iran, the Bush administration is hoping desperately to snatch an even bigger victory from the jaws of looming defeat in Iraq. A showdown with Iran, moreover, offers a way to outflank Democrats who have finally been pressured into showing real opposition to Bush's Iraq policy.<p>
For example, former Sen. John Edwards, one of the leading candidates for the 2008 Democratic presidential nomination, is combining rhetoric about ending the war on Iraq with a get-tough stance on Iran. "To ensure that Iran never gets nuclear weapons, we need to keep all options on the table," Edwards said. "Let me reiterate--all options."<p>
The longer the Democrats sit on the fence--against escalation in Iraq, but against an immediate withdrawal, too--the more room Bush has to maneuver.<p>
Even if the Democrats make good on threats to tinker with Bush's war budget to limit an increase in troops, a vote for the overall military appropriations bill--due in the next few weeks--would allow Bush to fund the war for the duration of his term.<p>
The split in the U.S. political establishment over Iraq is providing an opportunity for antiwar activists to expose the real aims of the U.S. war for oil and empire. The huge demonstrations in Washington, D.C., and other cities around the country on January 27 show the potential for building a movement that steps up the pressure on all the politicians.<p>

