<font face="Times New Roman, Times, serif" size="4">Hundreds of unionists demonstrate against budget proposal</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Protesting health care cuts</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Glenn Allen and Elizabeth Lalasz</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 2, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--About 700 people demonstrated January 29 at the Cook County Commission meeting downtown, one of a series of mandatory public hearings on proposed 17 percent cuts to public health services.<p>
Union contingents made up the bulk of the protesters, with large delegations from the Service Employees International Union (SEIU), the American Federation of State, County and Municipal Employees (AFSCME) and the National Nurses Organizing Committee/California Nurses Association (NNOC/CNA).<p>
The proposed cuts would mean 1,492 layoffs of county employees, closing 18 public health clinics, and trimming nearly half a billion dollars from the budget overall.<p>
This demonstration comes on the heels of a tumultuous week for the County Board. On January 22, nearly 1,500 county workers, including members of SEIU and NNOC/CNA and their supporters, packed the first public hearing on the cuts in Markham, a southwest suburb of Chicago. The protesters' palpable anger shook the room as they stood, chanting loudly, "No layoffs, no cuts!" and "Cut from the top!"<p>
Glenn Close, a patient at Oak Forest Hospital, bluntly stated the impact of the cuts: "People will die." Police were brought in to control the crowd, while hundreds of protesters were outside, unable to get in the hearing room.<p>
Another community rally was held on the West Side of Chicago, a mainly poor Black neighborhood, calling on everyone to protest the cuts.<p>
Jacqueline Reed, president and founder of the West Side Health Authority, spoke about why people need to fight. "I went to jail in 1965 in Mississippi because of civil rights, because they didn't care, and they thought they could get away with it," she said. "The purpose of this meeting tonight is to let them know they can't get away with it.<p>
"We see right now our people are scared all over the city. We see right now people calling and saying, 'I lost my job. Where can I go? I had health insurance. My doctor can't see me any more because I don't have insurance. My doctors are saying they want to care about me, but can't because they do not have a way to give me my medicine if they do see me.'<p>
"The Austin Wellness Clinic is not on the chopping block to close, but other clinics are. If I don't stand up for you, they will come for me. So we're in this together and we better care for one another."<p>
Just over the last week, this visible fight back has begun to force the county to reconsider these draconian cuts and try to find the money to cover the budget. John Stroger Jr. is now talking to Illinois Gov. Rod Blagojevich</FONT><FONT FACE = "Arial" COLOR = "#000000" STYLE = "font-size:12pt"> </FONT><FONT FACE = "Times" COLOR = "#000000" STYLE = "font-size:12pt">about obtaining more state funding.<p>
This positive development shows why we must keep organizing and building solidarity--to keep the pressure on the Cook County Commission as the late February budget deadline quickly approaches. We need to let them know that they can't get away with cutting public health care services to the bone.<p>

