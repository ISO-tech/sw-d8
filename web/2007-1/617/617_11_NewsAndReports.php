<a name="Stop"></a><font face="Times New Roman, Times, serif" size="4"><b>Stop deportations</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Steve Leigh</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--The Immigration and Customs Enforcement (ICE) finally succeeded in deporting Majid Al-Massari, a former employee of the University of Washington. Community support including pickets of his hearing delayed his deportation for more than two years.<p>
Majid is the son of a prominent Saudi dissident, Mohammad Al-Massari, who lives in London and leads the Committee for the Defense of Legitimate Rights. Majid's father and brother have been arrested and beaten by the Saudi government.<p>
Majid has sought asylum since 1997 based on this repression, but the government dragged its feet for seven years. Then, in July 2004, Majid was seized by ICE agents in part for "overstaying his visa"--the result of the government's delay in ruling on his asylum. Majid's co-workers in the School of Nursing had a government gag order imposed on them when they tried to find out what had happened to him.<p>
He was placed in solitary confinement, denied a shower for six days and not even allowed a copy of the Koran. His conditions eased somewhat after a picket organized by his co-workers, but he was confined until his deportation.<p>
At first, Majid was threatened with deportation under immigration law for a drug bust in 2003 and for overstaying his visa. Next, Homeland Security held him under "special provisions" related to terrorism. Now the government claims that Majid is a "security threat" because of his link to a "terrorist" organization--his father's human rights committee.<p>
Paul Soreff, one of Majid's lawyers, said that Majid's deportation is "because his father is a vocal critic of the Saudi Arabian government and royal family--a government closely tied to President Bush and his government."<p>
The Arab American Community Coalition, which has been organizing around Majid's case, also sees his deportation as a violation of his rights.<p>
Majid will likely be subject to arrest and physical abuse--or worse--in Saudi Arabia, a clear and compelling case for asylum. His deportation is another example of why we need to stop the "war on terror"--both for its death and destruction abroad and for its suppression of the rights of Arabs and Muslims and everyone else here in the U.S.<p>
