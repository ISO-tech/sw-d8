<p><a name="Police"></a><font face="Times New Roman, Times, serif" size="4"><b>Fight police brutality in Philadelphia</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Matt Pillischer</b></font><p>

<font face="Times New Roman, Times, serif" size="3">PHILADELPHIA--With fireworks still popping from New Year's celebrations, the notorious Philadelphia police killed yet another young Black man. Twenty-year-old Bryan Jones was shot dead on the morning of January 1, unarmed and reportedly running from the scene of gunfire.<p>
With three deaths in as many weeks into the New Year, 2007 looks like it has the potential to beat the 2006 record for killings by the Philadelphia police.<p>
On January 20, dozens of angry Philadelphians chanted and made speeches outside the 19th District police station. They carried signs mourning the fatal police shooting victims from last year, 20 in total, and urged sweeping measures to address the issue. "These cops in here are murderers, they're killers," one protester said outside the police station. A cop peeked out the window and laughed at the speaker, a friend of Bryan Jones.<p>
The second victim of the Philadelphia police this year is said to have been mentally ill, holding a knife, and was fatally shot while surrounded by police. The third, killed January 20, was just 16 years old. Police commissioner Sylvester Johnson is quick to point the finger at a "more violent street culture" to try to justify these killings.<p>
The protest Saturday was a promising beginning for a citywide movement to confront the police. With the killing of unarmed Sean Bell by the New York Police Department last fall and the huge protests across New York City that followed, the issue of police brutality can easily be connected, from city to city and state to state.<p>
We need to build multiracial coalitions in this city that can fight police brutality, as well as the factors that cause poverty, and aim to stop it.<p>
