<font face="Times New Roman, Times, serif" size="4">Guest-worker programs don't protect our rights</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Modern-day caste system</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 2, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">WITH THE new Democrat-controlled Congress getting started, and with a president eager to supply a steady flow of cheap labor to struggling American business, talk of an imminent comprehensive immigration reform is becoming louder. But the foggy environment in which it arises makes it a danger for undocumented immigrants.<p>
With the force of the U.S. Chamber of Commerce, Service Employees International Union and various powerful Hispanic civil rights groups behind it, the Alliance for Immigration Reform 2007 announced its formation recently to unify efforts to get a law passed before the politics of the upcoming presidential race make a deal impossible.<p>
Businesses are very concerned these days about their ability to find the labor force they need. The National Restaurant Association says jobs in food service are growing faster than the U.S. labor force, and according to the Associated Builders and Contractors, the industry needs 250,000 new workers each year to replace its aging workforce.<p>
The problem has been made greater by the increase in raids by Immigration and Customs Enforcement (ICE), the actions of local and state legislations, and the ratcheting up of the aggressive activities of far-right groups.<p>
For instance, last month, the ICE carried out a well-publicized raid against Swift and Co. meatpacking plants, arresting more than 1,000 workers; in Carpentersville, Ill., police have been authorized to perform immigration status checks; and the Minutemen mobilized recently to defend the right of border patrols to shoot immigrants in the back--even though those Border Patrol agents "were out to shoot Mexicans," as acknowledged by the Office of the Inspector General of the Homeland Security Department.<p>
About that case, U.S. Attorney Sutton declared that "being a United States Border Patrol agent is not a license to shoot people...It is not a license to write a report and turn it in which leaves out the fact that you shot an unarmed suspect who was running away from you."<p>
The shortage of labor created by these fear-mongering tactics, especially in the farmlands close to the border, has obligated the Senate to pass the Agricultural Job Opportunity, Benefits and Security Act of 2007, better known as the AgJobs bill, which would allow as many as 1.5 million undocumented farmworkers to gain legal status through a guest-worker plan.<p>
But that is exactly the model that proponents of "comprehensive immigration reform" on both sides of the aisle want to implement: using the crisis created by enforcement and intimidation to force the immigrant rights movement to accept legislation that includes temporary guest-worker programs. In other words, keep the hands...and lose the rights.<p>
Guest-worker programs leave workers at the mercy of the wishes of their bosses. Over the worker's head swings the ax of deportation at the boss' will, while the companies are provided with a cheap, unprotected labor force that helps them to lower the living and working standards of all workers.<p>
The infamous bracero program of the 1950s, described by author Justin Akers Chac&oacute;n as the "twentieth-century caste system," was a throwback to a bonded labor system where workers, according to the Presidential Commission on Migratory Labor of the time, had "to be ready to go to work when needed; to be gone when not needed."<p>
That is why two-third of those who marched in the massive immigrant rights demonstrations of last year--of which 73 percent were citizens--rejected any form of guest-worker program, according to the Immigrant Mobilization Research Project at the Latin American and Latino Studies department of the University of Illinois-Chicago.<p>
And, as the same study shows, they also marched for the legalization of all undocumented immigrants. That is nothing less than our movement should get. And just to make it clear, legalization means amnesty--and citizenship for those who want it.<p>
For this, our movement ought to adopt a strategy for the reconstruction of its ranks through bottom-up activism, creating networks for the defense of undocumented workers against raids and deportations, and confronting the right-wing bigots--rather than relying on the politicians in Washington, where we can only get a rotten deal.<p>
Ultimately, our struggle can only finish once the lines that divide the land are all abolished, when making just one step forward does not make us illegal, and when we recognize that, as Chicago journalist Ra&uacute;l Dorantes would say, we are citizens of the land on which we work.<br>
<b>Orlando Sep&uacute;lveda</b>, Chicago<p>

