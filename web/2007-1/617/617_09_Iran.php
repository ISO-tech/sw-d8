<font face="Times New Roman, Times, serif" size="5"><b>Warning signs of their next war?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 2, 2007 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">PRESIDENT BUSH'S January speech outlining his plans for a troop "surge" fingered Iran and Syria as causes of violence in Iraq. His State of the Union address also devoted space to attacking Iran and its supposed cat's paw in Lebanon, Hezbollah.<p>
Even many observers in the mainstream media are connecting the ramping up of anti-Iranian rhetoric to the possibility of a new war with Iran. So what is going on?<p>
Bush's "official" explanations--that Iran is "meddling" in Iraq, and that networks of Iranian agents are behind the resistance to the occupation--can be discarded. Coming from the chief meddler in Iraqi affairs, these allegations would be laughable if they weren't so serious.<p>
Relations between the Iraqi and Iranian governments are conducted quite openly, and many of the leading Shiite parties in Iraq today had branches or headquarters in Iran during the Saddam Hussein dictatorship. It is a matter of national security for the Shia-dominated Iranian government to have a "friendly" government in Baghdad it can coexist with.<p>
If the U.S. was really serious about "going after" foreign supporters of the networks that are attacking U.S. forces, it would be targeting its "allies" in Saudi Arabia, Jordan and the Gulf states, whose secret services are undoubtedly helping to fund the Sunni resistance.<p>
The real explanation for Bush's stepped-up campaign against Iran lies in the fact that Iran and, to a lesser extent, Syria are the last remaining holdouts to U.S.-Israeli domination of the region.<p>
Iran is the only country with sufficient natural resources and population depth to pose a challenge to U.S. allies in the region. An Iran with nuclear power makes it more powerful on the world hydrocarbon market. And an Iran with nuclear weapons undermines the U.S.-Israeli nuclear monopoly in the region.<p>
Since the U.S. lost its Iranian puppet, the Shah, in 1979, it has tried to undermine the Iranian regime. One key part of the strategy to accomplish this has been to build up a predominantly Sunni counterweight to the potential of Iranian domination of the Persian Gulf.<p>
The U.S., Saudi Arabia and Gulf states backed and encouraged Saddam Hussein's 1980-1988 war against Iran. When Saddam slipped the leash in 1990, the U.S. developed a policy of "dual containment" of Iran and Iraq.<p>
That policy began to fragment in the early 2000s, with influential politicians beginning to talk about "regime change" in Iraq. In the pre-Iraq war neoconservative salons, ideologues used to brag about their plans to empower the region's Shia to throw off anti-Western regimes.<p>
A "democratic" Iraq under secular Shia politicians like Pentagon favorite Ahmed Chalabi was to provide an example and jumping-off point for the U.S. to spread its influence--to overthrow the clerics in Iran and stimulate a move by oil-rich Shiite provinces in Saudi Arabia to declare independence from the monarchy.<p>
Of course, this vision was complete fantasy that crashed up against the reality of the spreading sectarian war in Iraq. So now, the neocons and their enablers in the Bush administration are reverting to the older colonial strategy of allying with the Sunni regimes against the "Shiite/Iranian/Persian" threat to their dominance.<p>
In other words, this U.S.-Israeli-promoted polarization of the region between Sunni and Shia wasn't a result of a sudden realization of Iranian designs in Iraq, Lebanon or anywhere else in the region. It is one piece of the template for Plan B (or Plan C or whatever) to save the U.S. venture in Iraq and its broader regional designs.<p>
Increasingly, it appears that this strategy is having the desired effect. In a recent <i>Asia Times </i>report, Amandeep Sandhu revealed that Saudi Arabia has boosted oil production with the express intent of lowering world oil prices and hurting Iran's economy.<p>
Moreover, Sandhu reports, Israel and Saudi Arabia have been engaged in secret talks that might be aimed at securing Saudi approval for Israeli overflight rights, should Israel opt to attack Iran's nuclear installations. And, according to Sandhu, "a financial war on Iran has already begun"--noting that the Iranian parliament concedes that the country's internal stability would be at stake if full economic sanctions were imposed.<p>
Despite the similarities with the buildup to the Iraq invasion, it's still too hard to say if the U.S. and/or Israel are preparing an imminent attack. But we should be alert to the warning signals that are flashing today.<p>

