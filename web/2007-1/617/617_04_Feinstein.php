<font face="Times New Roman, Times, serif" size="5"><b>Feinstein stands up for polluters</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 2, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">SEN. DIANNE Feinstein (D-Calif.) is doing everything she can to undermine even modest efforts to cut emissions of greenhouse gases.<p>
California made international news last year when AB32, which imposes stricter limits on both automobile and industry pollution, was passed. But Feinstein quickly jumped to pre-empt this legislation at the federal level.<p>
In a bill supported by PG&amp;E Corp. (famous for poisoning thousands of Californians, as detailed in the movie <i>Erin Brockovich</i>) and dozens of other energy conglomerates, she moved to exempt power companies from state regulations.<p>
Her justification was that power companies should be federally regulated, and not have to live up to the restrictions imposed by any particular state. Power plants account for a third of all greenhouse gas emissions.<p>
Feinstein was pressured by Gov. Arnold Schwarzenegger to take the clause out of the bill that would have pre-empted California's more stringent regulations, and she did remove it at the last minute with a promise to bring it back later.<p>
No matter. She's still doing the power companies a huge favor by writing fake "environmental" legislation that contains a "cap-and-trade" system. With cap-and-trade, particularly dirty companies simply have to buy the "right to pollute" from less dirty ones.<p>
This should turn your stomach. It is a more brazen, cynical and bald-faced attempt to head off and undermine genuine environmental reform than a satirist could imagine. It is made more disgusting because Feinstein's stump speech for her Senate race last year was based on the issue of global warming and the need to save the environment!<p>
The high school where I teach was invited to attend an event where she gave a keynote address on saving the environment. While she did manage to use the speech to get a few licks in against China ("They build a new coal-burning power plant every week"), she certainly didn't say anything to offend the Silicon Valley venture capitalists who have underwritten her "environmentalist" positions.<p>
Most <i>Socialist Worker</i> readers would probably expect the Democrats to turn a blind eye to the rapid destruction of our planet by industry. But from championing free trade and neoliberalism to undermining regulations on filthy industries, they are at the leading edge of the attack on the environment.<br>
<b>Sarah Knopp</b>, Los Angeles<p>

