<font face="Times New Roman, Times, serif" size="5"><b>Home front of their "war on terror"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 2, 2007 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THE HUNGER strike of a Palestinian activist has cast a much-needed spotlight on the federal government's witch-hunt against Arabs and Muslims.<p>
Dr. Sami Al-Arian, a former University of South Florida professor, has spent nearly four years behind bars on charges of aiding terrorism--despite the fact that the jury in his initial trial acquitted him on eight of the most serious charges and deadlocked on the others.<p>
Last year, with prosecutors threatening to retry him, Al-Arian agreed to plead guilty to a single count of providing material support for the nonviolent activities of a Palestinian charity that the U.S. government has deemed a terrorist organization.<p>
His recent hunger strike--his second since he has been imprisoned--was prompted in January after a judge found him guilty of contempt charges for the second time and extended his sentence again for refusing to testify in another case, even though his plea agreement specified he wouldn't have to testify.<p>
"In the past three weeks, I have been to four prisons," al-Arian reportedly told a judge. "I spent 14 days in the Atlanta penitentiary under 23-hour lockdown, in a roach- and rat-infested environment. On two occasions, rats shared my diabetic snack.<p>
"When I was transported from Atlanta to Petersburg, [Virginia,] and from Petersburg to Alexandria, they allowed me only to wear a T-shirt in subfreezing weather during long walks...In Petersburg, the guard asked me to take off my clean T-shirt and boxers, and gave me dirty and worn out ones. When I complained, he told me to 'shut the f up.' And when I asked why he was treating me like that, he said, 'Because you're a terrorist.'"<p>
Sami Al-Arian is one of many Arabs and Muslims hounded by the U.S. government since September 11--thousands have been rounded up and deported, arrested without cause or subjected to ongoing harassment, government "watch lists" and other indignities.<p>
The wave of anti-Arab and anti-Muslim bigotry is an integral part of the Bush administration's never-ending "war on terror." It has had real and devastating consequences--as the racist beating of three Palestinian students at Guilford College in North Carolina makes frighteningly clear. Documented hate crimes against Arabs and Muslims are sharply higher.<p>
Few Democrats, however, have stood up to anti-Arab and anti-Muslim hate. In fact, some have joined right in.<p>
In December, for example, Sen. Barbara Boxer retracted a "certificate of accomplishment" she had presented to Sacramento activist Basim Elkarra--when it turned out that Elkarra was a member of the Council on American-Islamic Relations, one of the most prominent Muslim-American civil rights organizations in the country.<p>
The Democrats now control both houses of Congress, but though they have been willing to criticize Bush's Iraq policy, party leaders have not made any move to roll back the administration's civil liberties-shredding measures.<p>
And no wonder. Though Bush stretched the law as far as possible with warrantless wiretapping and mail snooping, many of his most draconian measures--like the USA PATRIOT Act or the recent Military Commissions Act, which vaporized the right of habeus corpus--passed Congress with large numbers of Democrats signing on.<p>
The Democrats can't be trusted to turn the tide of attacks on our rights unleashed in the name of the "war on terror"--not when they've shown that they support that war at every turn.<p>

