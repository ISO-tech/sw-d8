<font face="Times New Roman, Times, serif" size="3">But instead of releasing him, prosecutors got a local dentist to give different testimony against Brown. Jurors were never told that bite-mark evidence has a 63 percent rate of false identification.<p>
After a decade in prison, Brown solved the case by himself while behind bars. He ordered all the police statements in the case, many of which his lawyer had never seen. The police had received a tip about another suspect, Barry Bench, but never pursued him--possibly because he was friends with the prosecutor.<p>
With this new evidence, and the help of the New York-based Innocence Project, Brown ordered the DNA tests that finally cleared his name.<p>
Roy Brown's nightmare is all too common in New York state. The latest exonerations of the wrongfully convicted reveal a history of forced confessions, junk science, rigged juries and suppressed evidence. But while other states have launched commissions to investigate such allegations, New York refuses to admit there's a problem.<p>
On February 6, the Campaign to End the Death Penalty and New Yorkers Against the Death Penalty will host a panel discussion to expose New York's shameful epidemic of wrongful convictions.<p>
Four exonerees will speak--Alan Newton and Jeff Deskovic, who spent half their lives in prison for crimes they didn't commit; Yusef Salaam, who was railroaded in the Central Park jogger case; and Lawrence Hayes, a former Black Panther framed for killing a police officer. They will talk about their experiences, and encourage more people to join the fight against the death penalty and the criminal injustice system.<p>

