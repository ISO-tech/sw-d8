<font face="Times New Roman, Times, serif" size="4">Another innocent man freed from prison in New York state</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"The wheels of justice are flat"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Dylan Stillwood</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 2, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">ROY BROWN spent 15 years in prison for a murder he didn't commit, but on January 23, he finally walked free after DNA evidence proved he was innocent.<p>
Facing reporters, Brown placed the blame on the whole criminal justice system. "Changes have got to be made, man," he said. "They say the wheels of justice move slowly, but you know what? The wheels of justice are flat."<p>
Brown is the eighth person in New York state to be exonerated by DNA evidence in the last 13 months. At least half of these cases involved misconduct by police or prosecutors.<p>
Brown's case is a stark example. The only witness against him was a jailhouse informant, and the only physical evidence were bite marks on the victim's body. Before the trial, one of the country's leading experts said the bite marks excluded Brown, who was missing two front teeth, as a suspect.<p>

