<font face="Times New Roman, Times, serif" size="5"><b>Amber Abreu faces prison for seeking an abortion</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 2, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">PROSECUTORS ARE calling Amber Abreu a murderer. But the 18-year-old is a victim of restrictions on access to abortion.<p>
Prosecutors recently charged Abreu, an immigrant from the Dominican Republic, with "procuring a miscarriage"--a felony that can carry a penalty of seven years in prison. They say they are planning to file additional charges, including a possible homicide charge, against her.<p>
In early January, Abreu, who was between 23 and 25 weeks' pregnant, took the drug Cytotec, a prescription anti-ulcer medication that is also used to induce labor, in order to induce a miscarriage. On January 6, two days after taking the pills, Abreu gave birth to a baby girl weighing just over 1 pound. The infant died four days later.<p>
Because abortion is illegal in Massachusetts after 24 weeks' gestation, prosecutors believe they may be able to pin homicide charges on Abreu.<p>
Cytotec and similar drugs are frequently used by women in Latin American countries where abortion is restricted by law. The drugs are seen as a cheap and somewhat safe alternative to expensive and dangerous back-alley procedures. In recent years, there has been a rise in reports of immigrant women in the U.S. turning to such drugs when they are unable to obtain an abortion for various reasons, including prohibitive cost or being too far along to obtain an abortion legally.<p>
In Abreu's case, she reportedly turned to the drugs out of worry that she and her immigrant mother could not handle "another baby to care for," said Abreu's cousin, speaking to the <i>Boston Herald</i>. According to reports, Abreu--who had had a legal abortion previously before--did not feel she could raise the money herself or ask her mother to pay, because the last abortion had cost $200.<p>
Abreu spent three nights in Framingham, a maximum-security women's prison, before her family could raise enough money to meet the $15,000 bail.<p>
Abreu's case is one of a number of cases in recent years where prosecutors have sought to punish women and their partners who are desperate enough to seek alternative methods of abortion when they find safe, legal ones out of reach.<p>
In 2005, 19-year-old Gerardo Flores was sentenced to life in prison in Texas after helping his girlfriend end her pregnancy. After being told she could not obtain an abortion, she asked him in desperation to repeatedly step on her stomach.<p>
In 2004, Gabriella Flores, a Mexican immigrant, was arrested in South Dakota for self-inducing an abortion using the same drug that Abreu used. Flores was initially charged with murder and spent four months in jail before the charges against her were reduced to violating a ban on "illegal abortions."<p>
Massachusetts prosecutors seem ready to punish Abreu for the same thing. "What is clear is that an inner-city teenager who is still studying English made a desperate choice when a safe and legal one proved inaccessible," wrote <i>Boston Globe</i> columnist Eileen McNamara. "This tragedy--and it is a tragedy--is less a measure of one teenager's bad choices than it is an indictment of a culture that tells all women abortion is their legal, constitutionally protected right, but tolerates a lack of access for the neediest women."<p>

