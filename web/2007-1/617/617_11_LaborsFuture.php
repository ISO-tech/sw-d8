<font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Immigrant workers are labor's future</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 2, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">SEVENTY YEARS ago this month, the upstart United Auto Workers union electrified the country with a victorious sit-down strike at General Motors plants in Flint, Mich., a bold action that opened the way to organize the auto industry and much of the rest of heavy industry.<p>
These days, the UAW is staggering from crisis to crisis, having agreed to major concessions in retiree health care and standing by as workers agreed to buyouts that will eliminate 92,000 jobs at GM, Delphi and Ford. The union that once set the pace for all of organized labor in terms of wages and benefits has agreed to cut pay just about in half for new hires at parts maker Delphi, to $12 or $14 per hour.<p>
The UAW's decline is the most striking example of U.S. labor's crisis, but there are many others--for example, in the airlines, where unions have failed to resist management's use of bankruptcy courts to roll back decades of workers' gains.<p>
In 2006, unions lost 326,000 members, leaving just 12 percent of workers in unions. Unions now represent only 7.4 percent of workers in the private sector, compared to about 35 percent in the mid-1950s.<p>
At the same time, however, there are signs of hope--most recently, the victorious strike by janitors in Houston and the walkout by workers at the Smithfield Foods pork processing plant in Tar Heel, N.C. In both cases, immigrant workers were key--a sign that the mass immigrant rights movement that exploded onto the scene in the spring of 2006 can find expression in organized labor itself.<p>
The question, however, is whether labor can continue to collaborate with management's effort to downsize and effectively cut industrial workers in half, while at the same time organizing low-wage and immigrant workers by fighting to improve their conditions.<p>
This dilemma was at the heart of the split in the AFL-CIO in 2005 that led to the formation of the Change to Win coalition of unions.<p>
The unions involved mainly represent workers in services, construction and transportation, where jobs can't be outsourced overseas. These include the Service Employees International Union (SEIU), UNITE HERE, the United Food and Commercial Workers (UFCW), the Carpenters, the United Farm Workers and the Teamsters.<p>
AFL-CIO President John Sweeney's only alternative, however, is the disastrous stay-the-course policy of "partnership" with management--what the militants of the 1930s used to call "class collaboration."<p>
On that basis, many have jumped on board the Change to Win bandwagon as labor's best hope for organizing the unorganized. But the main leader of Change to Win, SEIU President Andrew Stern, is himself a proponent of partnership with management--and has taken the concept to a new level with his support for a proposed guest-worker program for immigrants.<p>
Essentially, Stern has agreed that a group of workers will have substandard rights, in exchange for an opportunity for the SEIU and allied unions to represent them.<p>
The AFL-CIO, to its credit, opposes the guest-worker program. But it isn't clear whether the federation or its affiliates are willing or able to organize immigrant labor on a mass scale.<p>
And in an era where management shrugs off decent wages and benefits for unionized workers as unsustainable "legacy costs," the Change to Win unions, having left the AFL-CIO, seek to rid themselves of the legacy of defending those gains. And both wings of labor outsource politics to the Democratic Party, leaving the unions with no independent political voice.<p>
Labor leaders' continued paralysis is the result of their social position. Far removed from the pressures faced by workers and regularly associating with management and government officials, the labor bureaucracy is instinctively conservative.<p>
"For a couple of generations the American labor officialdom has been composed primarily of more or less adept politicians and negotiators, whose skills and outlook were acquired in inherited and well-ripened bureaucracies," labor activist and author Kim Moody wrote several years ago.<p>
"Unlike the [Sidney] Hillmans and [Walter] Reuthers of another era, whatever one thinks of them, the majority of today's labor leaders tend to be third generation 'yes men' (yes, men) with only a fading clone of a social vision handed down from International Executive Board to International Executive Board."<p>
The new activism by immigrant workers--and the efforts, however halting, by unions to organize them--represent the best hope of reversing labor's long decline.<p>

