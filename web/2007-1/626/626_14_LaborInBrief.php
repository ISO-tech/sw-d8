<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">April 6, 2007 | Page 14</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Oregon letter carriers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Adam Sanchez</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BEAVERTON, Ore.--More than 350 union letter carriers and supporters gathered at the Beaverton Post Office, just outside of Portland, on March 15 to protest the U.S. Postal Service's (USPS) attempt to outsource delivery.<p>
Earlier this year, Beaverton Postmaster John Lee informed the National Association of Letter Carriers Branch 82 in Portland that management intended to contract out 374 new delivery points in the area.<p>
According to Peter Shapiro, editor of the branch's monthly publication, "As many as 15,000 new delivery points are expected to open up inside the Portland urban growth boundary over the next few years, and apparently management considers them all fair game."<p>
In 2002, the new USPS board of governors, appointed by the Bush administration, changed its rules about using private carriers to handle "highway contract service" where the mail was delivered in "sparsely populated areas."<p>
Under the new rules, the Postal Service dropped the "sparsely populated" requirement opening up all routes to private contractors. Since 2002, "highway contract routes" have been contracted out in areas with average-to-high population densities in Washington, Nevada, Arizona and Florida.<p>
These contracts typically pay less than half the salary of a union letter carrier, with no sick leave, vacation, health insurance or retirement. According to Branch 82 union president L.C. Hansen, private letter carriers could be a step by the USPS to create "Wal-Mart-type, low-pay jobs with no benefits."<p>
Hansen said, "It's the first time in years that the union has set up informational pickets outside a post office." While Branch 82 plans to file a complaint against the postmaster, Hansen expressed serious doubt that the union could win anything through the national arbitration process. In a report to the union she urged, "Our ultimate tool is the court of public opinion."<p>
