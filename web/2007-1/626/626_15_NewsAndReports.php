<a name="NEIU"></a><font face="Times New Roman, Times, serif" size="4"><b>Northeastern Illinois University</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Adam Turl</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--The struggle for free speech at Northeastern Illinois University (NEIU) continues. Campus disciplinary charges have been filed against two members of the Campus Antiwar Network (CAN) and NEIU Socialist Club.<p>
On February 28, two members of the Socialist Club were arrested for taking part in a CAN protest of a campus CIA recruitment event. Ken Barrios and Matt Larson were erroneously charged with "disruption" and "simple battery." Some members of the Student Government Association (SGA) seized the opportunity to go on witch-hunt against the Socialist Club--but backed down in the face of an overwhelming outpouring of support.<p>
Activists then turned their attention to the pending court case against Barrios and Larson. On March 28, 40 people attended a CAN press conference on campus to demand the charges be dropped. Later that day, 20 supporters packed the court hearing in downtown Chicago.<p>
The administration has reportedly offered to drop charges if Barrios and Larson promised not pursue a civil rights lawsuit against the university. However, the Chief Clerk of the Placement Office, Robin Wagner, who filed the bogus complaint of "simple battery," refused to withdraw charges. The State's Attorney downgraded the battery charge against Barrios, but, as <i>Socialist Worker</i> went to press, all charges remained in place.<p>
A disciplinary complaint was filed against Larson and Barrios on March 27 by Dean Kelly. A hearing will be held on April 9 at 10 a.m. Outrageously, if Larson and Barrios invoke their Fifth Amendment rights, they could be suspended on the spot--even though they face an ongoing trial.<p>
This ham-fisted intimidation is obviously meant to intimidate student activists into backing down, but students are vowing to fight. A protest is being organized by CAN on April 9 to demand all criminal and campus charges be dropped.<p>
<i>What you can do to help: Attend the protest at NEIU on April 9 at 9 a.m., in the NEIU Quad, 5500 N. St. Louis. Call Dean Kelly's office at 773-442-4610 and ask them to drop all charges now. Come to the next court hearing on April 24 at 1:30 p.m., at 555 W. Harrison</i>.<p>
