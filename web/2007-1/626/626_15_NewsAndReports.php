<p><a name="NYC"></a><font face="Times New Roman, Times, serif" size="4"><b>New York City police violence</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Andrew Jagunich</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Police here are trying to smear the memory of Sean Bell, who was gunned down in a hail of 50 bullets on November 25, 2006. Three of the five officers involved in the killing now face criminal charges.<p>
NYPD Commissioner Raymond Kelly claims that a Queens man, Anthony Jeffers, identified Bell as the person who attacked him in a drive-by shooting last July. The press immediately repeated Kelly's statement, with the <i>New York Post</i> running an article with the headline "Sean Bell Shot Me."<p>
Jeffers, however, denies that he ever said Bell did it, and says he never saw Bell's face until cops showed him a photo. "They asked me, 'Did he do it?' I said no," Jeffers told the <i>New York Times</i>. "It's a false statement. They're lying."<p>
On March 16, a Queens grand jury indicted Officers Gescard Isnora and Michael Oliver for manslaughter, and charged Detective Marc Cooper with reckless endangerment.<p>
Just days before, the Detectives' Endowment Association claimed to have found a worker at a AirTrain platform who allegedly saw someone shoot at the police on the night of Sean Bell's death. However, the Air Train platform is roughly a mile away from the club where Sean Bell was killed.<p>
Anti-police brutality activists are seeing through the smear campaign against Sean Bell. A March 20 rally at Union Square called by People's Justice drew 200 people and demanded that all five cops be charged with murder.<p>
"The 50 bullets is not the issue," said Juanita Young, who lost a son to police violence. "It's just the fact that they're so eager to pull that gun."<p>
Michael, a construction worker from Brooklyn, said in an interview that "this is not just a New York City problem, it's a nationwide problem. Crime happens because of economic problems. They shouldn't flood the neighborhood with police, they should flood it with jobs."<p>

