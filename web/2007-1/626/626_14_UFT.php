<font face="Times New Roman, Times, serif" size="5"><b>Election results reflect UFT crisis</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Megan Behrent, UFT</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 6, 2007 | Page 14</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Elections in the United Federation of Teachers (UFT) ended with the re-election of the current president, Randi Weingarten, and her Unity caucus retained a strong majority.<p>
Nonetheless, modest gains were made by Independent Community of Educators-Teachers for a Just Contract (ICE-TJC), the opposition caucuses in the UFT which formed a coalition to challenge the incumbents. The vote for the opposition, amid a record-low voter turnout, reflects increasing anger and disillusionment with the current leadership and direction of the UFT.<p>
The total number of people voting was much lower than three years ago (which was already low, with only about 30 percent of the active membership voting). Thus, while over 160,000 ballots were sent out, only about 45,000 were returned. Of these, 22,000 were from retirees, which means that less than a quarter of the active membership of the union voted in this election.<p>
This low turnout in part reflects the complete disillusionment with the union leadership and the UFT leadership's failure to publicize the vote. Furthermore, the American Arbitration Association sent out faulty ballots that, while eventually corrected, created a great deal of confusion.<p>
ICE-TJC ran a joint campaign, opposing givebacks in recent contracts that have led to a longer workday, longer work year and eroding rights in the workplace. The opposition argued instead for a strategy of militant rank-and-file organizing in the union.<p>
Throughout the elections, the Unity caucus tried to attack ICE-TJC and its presidential candidate, Kit Wainer, through red baiting.<p>
In a postcard sent to large sections of the membership, Unity accused TJC of being "stuck" in the 1930s and warned members against voting for a "militant socialist" who "advocates strikes and strike threats for political and ideological purposes." Even though TJC is not a socialist formation, the caucus defended the role of socialists in the unions.<p>
"Weingarten and Unity want to say that militant socialism is back in the 'last century,'" a TJC flyer stated. "That's right: the century when the UFT itself organized through a series of militant strikes, when our union made real progress for members."<p>
Despite Unity's dominance, its support declined, whereas ICE-TJC made gains. The opposition ICE-TJC got almost 20 percent of the votes from the active membership of the union (excluding retirees) and saw an increase in all divisions, gaining 20 percent of the Intermediate School vote, about 16 percent of the elementary school vote and 36 percent of the high school vote.<p>
While ICE-TJC lost the six high school executive board seats they held for the past three years, this was expected. Since Unity did not run for these positions in the last election as part of a deal with the former opposition caucus, New Action, ICE-TJC was able to win.<p>
For Teachers for a Just Contract, the election campaign was about organizing rank-and-file teachers in our union. While the outcome may seem disappointing to some rank-and-file members of the union, the gains demonstrate both the possibility and necessity for mobilizing the rank and file to fight back against the increasing attacks on our working conditions and our schools.<p>
The process of rebuilding and reforming our union from below is part of a long-term process that will require us to build on the gains from this election to expand and train TJC's base of rank-and-file union members. The opposition needs to continue to push the leadership to fight for better working conditions and to stop the assault on schools from Mayor Michael Bloomberg and Chancellor Joel Klein.<p>
The UFT leadership has begun to fight against the mayor's school reorganization plan, which includes privatization and an emphasis on testing. The UFT has a citywide demonstration May 9 to put the "public back in public education." This is a good start, but the opposition will have an important role to play.<p>
The challenge now is to push the UFT leadership to take on the mayor's assault on contractual tenure and seniority rights and to develop a strategy that can stop the overall assault on public education, and begin to make real gains in improving working conditions in our schools.<p>
