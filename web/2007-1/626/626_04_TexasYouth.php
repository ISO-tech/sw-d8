<font face="Times New Roman, Times, serif" size="5"><b>Scandal shakes Texas youth prisons</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Cindy Beringer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 6, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">SHAQUANDA COTTON, the 14-year-old African American given a seven-year sentence in a Texas youth prison for shoving an aide at her school, walked free--as a spreading scandal shook the Texas Youth Commission (TYC).<p>
Shaquanda's release is a terrific victory for the activist campaign begun by her mother in protest of the absurd and racist sentence she was given--despite the fact that she had no prior arrests, no serious discipline violations at school, and the school aide she allegedly pushed suffered no serious injury.<p>
Three months before Shaquanda was tried, the same judge presided over the trial of a white girl the same age as Shaquanda, who was convicted of arson for burning down her family house. The white youth got probation.<p>
The case underlines the injustice of a system that is under fire because of allegations of sexual abuse and blackmail at youth prisons.<p>
Texas Gov. Rick Perry is under pressure to clean house after revelations that between 60 and 90 percent of youth incarcerated in the system had their sentences extended beyond the minimum nine months--many without proper documentations, and some because the youths refused to have sex with guards and prison officials.<p>
Hundreds of the 4,600 prisoners in the youth system could be released in the coming weeks, according to press reports.<p>
Rumors of sexual abuse and unusual nighttime meetings between students and the assistant superintendent and principal of the West Texas State School (TYC calls its jails "state schools") had circulated for some time. In 2005, Marc Slattery, a volunteer math tutor at the facility who has since been fired, was approached by students who wanted to talk with him about "something icky."<p>
Frustrated by the agency's unwillingness to investigate itself, Slattery called in the Texas Rangers and the FBI, which corroborated the allegations of sexual abuse through statements and DNA samples of semen on rugs at the facility.<p>
Yet no charges were filed, and nothing was done until the scandal broke out publicly in February, thanks to articles in the bimonthly <i>Texas Observer</i>.<p>
Further reports of sexual and physical abuse have surfaced in other TYC jails. There are now reportedly more than 500 cases under investigation.<p>
Like the West Texas facility, TYC jails are built in remote locations that make visits and oversight difficult. Overcrowding and understaffing add to the potential for abuse. The age range of "students" runs from as young as 11 to 21, the age of adulthood in Texas.<p>
Guards and officials at TYC jails have been charged with extending the sentences of children who refuse to perform sexual favors for the staff. Sentences have also been extended for inmates who have sought help or filed formal complaints against the system.<p>
As a result, prisoners in TYC facilities serve more time than adults convicted of the same "crime."<p>
For example, Shaquanda Cotton's sentence was extended because officials found contraband in her cell--an extra pair of socks. Marquieth Jackson, who was locked up when he was 12 in 2003, has amassed 664 violations for violent behavior that he probably can't control--he has been diagnosed with schizo-affective disorder and depression, but TYC officials insist he can "control himself" if he tried harder.<p>
At least half of the young people in TYC have some type of mental problem, according to the <i>Austin American Statesman</i>, and others have some level of mental retardation--statistics that mirror the adult prison population in Texas.<p>
Many judges say they have no option other than sentencing youth to TYC. Local communities that are short on funds for providing social services are happy to let the courts take over for those who slip through the cracks.<p>
So far, Gov. Perry has refused calls to appoint a conservator to overhaul the system. And few people trust the man he appointed as special master, Jay Kimbrough, to take action. At the time the allegations were emerging from TYC facilities, Kimbrough was working in the state attorney general's office and governor's office--he may have been involved in the cover-up.<p>
A special panel with representatives of the ACLU, NAACP and League of United Latin American Citizens is set to review the cases of all 4,600 young people in the TYC system--and will release those whose sentences have been improperly extended.<p>

