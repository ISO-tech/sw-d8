<p><a name="Risk"></a><font face="Times New Roman, Times, serif" size="4"><b>At risk for feminism</b></font><p>

<font face="Times New Roman, Times, serif" size="3">FEMINISM IS bad for your health--according to the mainstream media, anyway.<p>
Last month, researchers in Sweden released a study that compared data on nine indicators of gender equality in both the private and public sectors, ranging from the proportion of men and women in management jobs to average income, local life expectancy, disability and absence from work due to illness.<p>
"The results showed a strong link between gender equality and levels of sickness and disability for both men and women," reported Britain's <i>Independent</i>. "One of the findings was that equal financial resources between the sexes was associated with higher levels of sickness and disability. For both sexes, gender equality in managerial positions was associated with lower life expectancy.<p>
"The scientists said possible explanations for the correlation is that men's health may be adversely affected by a loss of what had been seen as traditional male privileges. Women's health, meanwhile, could be being damaged by greater opportunities for risky behavior as a result of increased income, along with stress from longer working hours."<p>
Though little additional information was given, RushLimbaugh.com drew the obvious conclusion: "Feminism makes people sick." The liberal British <i>Independent </i>newspaper was more qualified. "Why feminism could be bad for your health," read its headline.<p>
As <i>Guardian </i>columnist Zoe Williams pointed out, "Is equality a prime factor here, or is it simply that people with managerial jobs--male and female--work harder and drink more and go to bed later than people without?"<p>
"Even if gender parity increased your risk of illness by a factor of 100 percent," Williams added, "what do they think would happen--women would all resign our jobs and resume knitting?"<br>
--<i><b>The Guardian</b>, </i>March 28, 2007<p>
