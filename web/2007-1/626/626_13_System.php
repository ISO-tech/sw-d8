<a name="McJobs"></a><font face="Times New Roman, Times, serif" size="4"><b>What's really wrong with McJobs?</b></font><p>

<font face="Times New Roman, Times, serif" size="3">MCDONALD'S IS McAshamed. The fast food giant is lobbying the Oxford English Dictionary (OED) to eliminate the word "McJob" from the dictionary.<p>
As many as one in every eight workers in the U.S. has spent time behind a McDonald's counter. Low pay, bad hours and little prospect for a stable future lead to high turnover--thus, the coining of the term "McJob."<p>
According to the OED, a McJob is "An unstimulating, low-paid job with few prospects, esp. one created by the expansion of the service sector." The Merriam-Webster dictionary defines McJob as "a low-paying job that requires little skill and provides little opportunity of advancement."<p>
"Dictionaries are supposed to be paragons of accuracy," Walt Riker, a company spokesman complained to the Associated Press. "And it this case, they got it completely wrong. It's a complete disservice and incredibly demeaning to a terrific workforce and a company that's been a jobs and opportunity machine for 50 years."<p>
What Riker and other McDonald's executives ignore, however, is that the term "McJob" isn't a comment on the quality of McDonald's employees, but the lackluster employment opportunities the company provides.<p>
In fact, the OED also has an entry for the entire "Mc" prefix, defining it as a depreciative prefix attached "chiefly to nouns to form nouns with the sense 'something that is of mass appeal, a standardized or bland variety."<br>
--<i><b>Der Spiegel</b></i>, March 21, 2007<p>
