<font face="Times New Roman, Times, serif" size="5"><b>The Revolutionary Ideas of Karl Marx</b></font><p>

<font face="Times New Roman, Times, serif" size="3">Time and again, Marxism has been declared dead, and capitalism the final form of human fulfillment. Yet every generation--the workers of the great U.S. union struggles of the 1930s, student radicals in the 1960s, the millions who struggled to overthrow apartheid in South Africa, or those who joined the Solidarity movement in Stalinist Poland--has rediscovered the "buried" ideas of Marxism as a way to understand the world around them.<p>
