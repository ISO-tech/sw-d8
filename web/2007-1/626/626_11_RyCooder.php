<font face="Times New Roman, Times, serif" size="5"><b>In search of strikes, beer and music</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Jim Ramey</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 6, 2007 | Page 11</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Ry Cooder, <b><i><a href="http://www.amazon.com/gp/redirect.html?ie=UTF8&amp;location=http%3A%2F%2Fwww.amazon.com%2FMy-Name-Buddy-Ry-Cooder%2Fdp%2FB000MDH8E6%3Fie%3DUTF8%26s%3Dmusic%26qid%3D1175594725%26sr%3D8-1&amp;tag=socialistwork-20&amp;linkCode=ur2&amp;camp=1789&amp;creative=9325" target="_blank">My Name Is Buddy</a>,</b></i> Nonesuch, 2007.</font><p>

<font face="Times New Roman, Times, serif" size="3">RY COODER'S newest album, <i>My Name Is Buddy</i>, tells the story of Buddy the Red Cat, Lefty Mouse and the Rev. Tom Toad, a trio of rabble-rousing animals who travel the country in search of birthday parties, labor strikes, accordions and beer.<p>
Matching up well with the last two albums Cooder made, <i>Buena Vista Social Club</i> and <i>Chavez Ravine</i>, <i>My Name Is Buddy </i>tells the hidden story of labor struggles throughout the U.S.<p>
Borrowing from a myriad of sources, Cooder gives a special nod to country music with songs like "Footprints in the Snow," a reworking of a Bill Monroe song; "Hank Williams" about the celebrated singer and songwriter; and the centerpiece of the album, "Three Chords and the Truth," which is what songwriter Harlan Howard said gave country music its appeal.<p>
Handled by a lesser artist, the addition of three animals into this history and music lesson could be viewed as ridiculous, but from Cooder, the effect is beautifully subversive. In one example, "Cat and Mouse," Buddy tells the story of how he, a cat who had been told since he was a kitten not to trust mice, came to be traveling with Lefty, a mouse.<p>
Buddy was traveling alone and got caught in a snowstorm with a suitcase for shelter but no food. Lefty, who knew the trick to get the cheese from mousetraps, had plenty of food but no shelter.<p>
Pairing up, Lefty shares his knowledge of the world with Buddy, "They'll tell you lies to make you doubt your fellow man/Like cats and mice just can't get along/It suits the bosses Buddy and it serves them fine/'Cause it keeps us working folks from being strong."<p>
Stories of class solidarity, racist vigilantes and disenfranchisement abound. Rev. Tom Toad comes to a town that's been overrun by the Ku Klux Klan in "Sundown Town." Buddy refuses to tell the cops where Lefty and Tom are, even when threatened with deportation in "Red Cat Till I Die." Buddy tells the story of a man-eating pig named J. Edgar.<p>
The highlight of the album is "Three Chords and the Truth." The importance of the song is that it doesn't give the credit to the Nashville country music industry that imposed this quote as scripture on all its artists, but to the artists who really did speak the truth to audiences.<p>
Naming Joe Hill, Paul Robeson and Pete Seeger, Cooder consciously picks artists who were persecuted by the U.S. government and pays them tribute for using popular music to support progressive causes, saying, "The only crime they done was three chords and the truth."<p>
Cooder was number eight on the recent <i>Rolling Stone </i>list of top 100 guitar players. In his last three albums, his work on the guitar has been more understated and sweet at times, but not the transcendent work he created in the 1970s and '80s.<p>
Despite this, these albums represent some of the most important work created by a contemporary musician. That combined with his outspoken left-wing politics make Cooder, who turned 60 last month, one of the most exciting figures in popular music.<p>

