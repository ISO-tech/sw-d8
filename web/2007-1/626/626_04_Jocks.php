<font face="Times New Roman, Times, serif" size="3">Just 17 years old when he was convicted, Gary was the youngest person on death row in the U.S. His death sentence was commuted to life in prison when the Louisiana death penalty was later ruled unconstitutional.<p>
Although at the time his case attracted the support of prominent activists, including civil rights leader Rosa Parks, Tyler has remained incarcerated to this day, with all bids for parole rejected.<p>
Recently, public awareness of Gary's case has been renewed, first with an article in the <i>International Socialist Review</i> last year, and then a series of stories earlier this year by <i>New York Times</i> columnist Bob Herbert. In February, Amnesty International renewed its call for Tyler's release, saying that his original trial was "infected with racial prejudice."<p>
Here, we print the Jocks 4 Justice letter calling for Gary Tyler to be released from unjust imprisonment.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>To: Gov. Kathleen Blanco</b><br>
We, the undersigned members of the sports community, call upon you, in the name of justice and racial reconciliation, to pardon Gary Tyler and free him from Angola prison.<p>
Gary is an innocent man who has spent 32 of his 48 years on earth behind bars for a crime he did not commit. Gary's life has been destroyed because of racial hysteria and that peculiar brand of police work known internationally as "Southern Justice."<p>
As you are undoubtedly aware, <i>New York Times </i>columnist Bob Herbert has spent the last month exposing the terrifying truth behind Gary's conviction. In 1975, Gary Tyler, an African American teenager, was convicted by an all-white jury for the murder of Timothy Weber, a 13-year-old white youth. Weber was shot and killed during a busing riot where 200 whites attacked Gary's school bus.<p>
Weber's death quite understandably sent shock waves across the state. The police needed a killer. They chose Gary, and his nightmare officially began.<p>
Gary's mother detailed to Herbert the sounds of listening to deputies at the police station savagely whipping her son, while they blocked her from entering the room. "They beat Gary so bad," she said. "My poor child. I couldn't do nothing." Every witness who identified Gary as the shooter has since recanted and alleged police intimidation.<p>
The gun supposedly used on that day has disappeared. In the mid-1970s, Gary's case mobilized thousands across the country for his freedom and led Amnesty International to declare him a "political prisoner."<p>
Denied a fair trial 32 years ago, imprisoned for life for a crime he did not commit--we call upon you to free Gary Tyler now.<p>
