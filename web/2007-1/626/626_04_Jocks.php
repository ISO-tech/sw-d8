<font face="Times New Roman, Times, serif" size="5"><b>Athletes speak out for a victim of injustice</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 6, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">A GROUP of athletes and writers calling themselves Jocks 4 Justice is rallying behind the case of a victim of the racist Louisiana justice system.<p>
An online column by sportswriter and <i>Nation </i>contributor Dave Zirin introduced a letter asking Louisiana Gov. Kathleen Blanco to pardon and free Gary Tyler--who has spent more than three decades behind bars in the infamous Angola prison for a crime he did not commit.<p>
In 1975, Tyler, an African American teenager, was wrongly convicted of the murder of Timothy Weber, a 13-year-old white youth. Weber was killed in 1974 during an attack by a racist white mob on a school bus filled with African American high school students in Destrehan, La.<p>
Tyler's trial was typical of "Jim Crow" justice in the South: In addition to being tried in front of an all-white jury, several witnesses later recanted, claiming police coerced their testimony.<p>
The gun supposedly used in the killing--a .45 automatic later found to have been stolen from a firing range used by police--had no fingerprints on it. The bullet that supposedly came from the gun was never tested to see if it had passed through a human body.<p>

