<font face="Times New Roman, Times, serif" size="5"><b>How military families support loved ones</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 6, 2007 | Page 10</font><p>

<font face="Times New Roman, Times, serif" size="3">RECENTLY, THE <i>Boston Globe</i> printed a pro-war op-ed article by columnist and right-wing ideologue Jeff Jacoby. Jacoby, whose pieces typically drip with anti-Arab and anti-Muslim racism, used this occasion to attack antiwar activists who voice support and concern for U.S. troops while opposing the war.<p>
Maureen and Michael Hearn; Carlos and Melida Arredondo; Karen Murphy, RN; Sarah and Robert Fuhro; Anne Chay; Kanchan Gokhale; Robert Froelich; and Khury Petersen-Smith--members of the Massachusetts chapter of Military Families Speak Out (MFSO)--sent this response to the <i>Globe</i>:<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">ON FEBRUARY 18, 2007, in an opinion piece written by Jeff Jacoby, he argued that it is impossible both to oppose the U.S. occupation of Iraq and support the troops who are in Iraq as part of the occupation. Jacoby explained his logic through examples, saying, "no loyal Colts fan rooted for Indianapolis to lose the Super Bowl. No investor buys 100 shares of Google in the hope that Google's stock will tank."<p>
We take offense to Jacoby's trivialization of our concern for our loved ones and our opposition to the war by comparing them to sports and finances.<p>
Members of the Massachusetts Chapter of Military Families Speak Out met on February 19, 2007, the day after Jacoby's opinion piece was printed in the <i>Globe</i>. In that room sat mothers, fathers, stepmothers, cousins and sisters of the troops who have served, are currently serving, or have been killed in Iraq.<p>
Our conversation on Monday included discussion of how the troops currently serving purchase their own protective equipment, troops returning from combat encounter difficulties in accessing medical care, and how one Massachusetts Marine was killed during battle when his company had run completely out of ammunition.<p>
There is a great deal of hypocrisy involved in how the troops are not provided adequate resources while in the battlefield, are left on waiting lists for medical care, and are provided rat-infested conditions at the lodgings where they are expected to recuperate.<p>
The Iraq war has been based on now admittedly faulty intelligence that has no defined enemy or definition of what success entails and deserves a healthy amount of questioning by all citizens. It has resulted in billions of dollars being unaccounted for. National defense now accounts for 59 percent of total discretionary spending while veterans' benefits and services account for 4 percent (and continue to get cut).<p>
As for the war, it has been lost. Now there is rampant civil war between various Iraqi factions--a civil war our presence helped ignite--and our soldiers are being killed in that civil war while providing a touchstone for continued enmity between those Iraqi factions.<p>
Question: Do we show our support for our troops by leaving them to die in a conflict that has no purpose other than to save face for those who began it?<p>
As a recently returned Iraq veteran wrote to his Massachusetts Congressman, "I do not believe that [voting against the escalation] is enough. I do not believe that Iraq can be salvaged. I do not know what victory looks like in Iraq, and I feel that the current policies have failed. I do not see how the sacrifice of more American lives will help to stabilize the situation. I would ask you to move forward in whatever way you see fit to disengage the United States from this terrible debacle as soon as possible."<p>
This soldier articulated the views of many troops fighting in Iraq. Last March, <i>Stars and Stripes</i> reported that 72 percent of troops in Iraq wanted the U.S. to get out within a year.<p>
Forcing the troops to continue fighting a war that they themselves are increasingly questioning and opposing is not "supporting" them, it is the opposite. The troops "surely...deserve better than pious claims of 'support' from those who are eager" to continue the Iraq conflict.<p>
Please note the increase in AWOL cases by the troops, conscientious objectors and those signing on to "Appeal for Redress" (a formal appeal made by an individual service members to their congressional representatives and U.S. senators to urge an end to the U.S. military occupation).<p>
Knowing that our loved ones face horror, possible injury and death each day, we know that the only way to support them is to bring them home now.<p>

