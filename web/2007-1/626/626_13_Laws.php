<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The line between legal and illegal</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 6, 2007 | Page 13</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>PAUL D'AMATO explains how laws buttress the interests of the dominant class.</b><p>

WHAT IS the origin and purpose of law? Many would answer with some kind of general platitude about how laws maintain and regulate social order.<p>
The pompous, arcane and abstract character of law conveys the idea that it stands above the social fray and is an expression of eternal truths corresponding to a set of moral precepts that stand for all times and occasions (It is also written this way, so that only professional lawyers can make heads or tails of it).<p>
Yet many pre-class societies got along fine without any written laws and without any system of legal coercion to enforce accepted social norms. Moreover, laws have changed through history and between different types of societies.<p>
In feudal England, poor farmers were afforded rights to "common" land where they could graze their animals, cut firewood and farm. As capitalist market relations began to develop between the 16th and 18th centuries and the demand for "free and rightless" labor increased, laws were passed that sanctioned the confiscation of millions of acres of common lands by landowners and the removal of peasants from the land.<p>
What accounts for these differences? According to Karl Marx, "Each special mode of production and the social relations corresponding to it, in short, ...the economic structure of society, is the real basis on which the juridical and political superstructure is raised."<p>
Laws are a product of the economic structure of society--they buttress the interests of the dominant class.<p>
The institution of slavery in the South, for example, did not develop because laws existed that made slavery legal. In fact, slavery did not catch on in the North even when it was legal. As historian Kenneth Stampp notes:<p>

<blockquote>
The use of slaves in southern agriculture was a deliberate choice (among several alternatives) made by men who sought greater returns than they could obtain from their own labor alone, and who found other types of labor more expensive. "For what purpose does the master hold his servant?" asked an ante-bellum Southerner. "Is it not that by his labor he, the master, may accumulate wealth?"
</blockquote><p>

The legal structure sanctioning slavery and the almost absolute control of the master over his slaves developed as a result of these economic developments.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">BUT LAWS don't always enforce ruling interests directly. Often, the form a law takes obscures its economic roots.<p>
Just as the state, though it enforces the interests of the dominant exploiting class, appears to stand over and above society, law appears in the same way, as a fair arbiter between all of society's members.<p>
This is especially true under modern capitalism. Bourgeois analysts see the state and its laws as a force that must intervene to ensure that the exchange between commodity producers proceeds in a peaceful and orderly manner.<p>
The conception of law as providing equal conditions and obligations for all is a product of capitalist society. If in economic relations, the formal equality of market exchange masks the appropriation of surplus labor by the capitalist, in legal relations, the formal equality recognized under the law disguises the fact that the law ultimately enforces unequal economic and social relations between a minority of big capitalists (the owners of means of production) and the majority of producers (who only own their own labor power).<p>
Therefore, laws that help perpetuate class inequality aren't necessarily a direct reflection of those divisions. There are no laws written down that explicitly favor the wealthy, though their practical effects show that they do. For example, laws against vagrancy, theft, panhandling, etc., are laws that apply equally to everyone who commits them--except that if you are wealthy, you have no need to commit any of these "crimes."<p>
Yet at the same time, the perfectly legal functioning of the economy leads to the routine fleecing of the majority by the minority. The radical lawyer Clarence Darrow, in a speech to Cook County prisoners in 1902, made the following observation:<p>

<blockquote>
There is no doubt there are quite a number of people in this jail who would pick my pockets. And still I know this, that when I get outside pretty nearly everybody picks my pocket...[W]hen I want to light my house or my office, the gas company holds me up. They charge me one dollar for something that is worth 25 cents.
</blockquote><p>

Darrow understood the purpose of the law better than most in his profession:<p>

<blockquote>
Those men who own the earth make the laws to protect what they have. They fix up a sort of fence or pen around what they have, and they fix the law so the fellow on the outside cannot get in. The laws are really organized for the protection of the men who rule the world. They were never organized or enforced to do justice.
</blockquote><p>

Ewan McColl's song "Legal-Illegal" makes a similar point:<p>

<blockquote>
It's illegal to rip off a payroll,<br>
It's illegal to hold up a train,<br>
But it's legal to rip off a million or two<br>
That comes from the labor that other folk do.<br>
To plunder the many on behalf of the few<br>
Is a thing that is perfectly legal.
</blockquote><p>

