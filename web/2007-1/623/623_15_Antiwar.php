<font face="Times New Roman, Times, serif" size="5"><b>Wash. activists try to block weapons shipments</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jesse Hagopian</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 16, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--"We are here to disrupt the war machine," Tom McCarthy told <i>Socialist Worker</i>, as he related the events of the weeklong demonstrations at the Port of Tacoma against the shipment of 300 Stryker tank vehicles to Iraq.<p>
The shipment of tanks comes in advance of the deployment of the 4th Brigade, 2nd Infantry Division from neighboring Fort Lewis as part of a military surge into Baghdad.<p>
The demonstrations have been called by the newly formed Tacoma chapter of Port Militarization Resistance (PMR)--an organization that formed last spring in Olympia, Wash., to oppose similar shipments of tanks to Iraq.<p>
The demonstrations reached a high point on Friday evening when over 200 people from throughout western Washington and Oregon gathered at the Federal Courthouse in Tacoma to rally against the war.<p>
After the rally, hundreds of activists converged on the Port of Tacoma to continue to demonstrate their opposition to the military shipments. The protesters were met by over 100 police in full riot gear who were determined to disrupt and intimidate.<p>
"From the very beginning the cops have been there to stifle the anti-port-militarization movement," said Jeff Berryhill, of the Olympia PMR, who was shot in the leg with a rubber bullet at one point during the protests.<p>
Navy veteran Wally Cuddeford described the brutality of the police on the <i>Democracy Now </i>radio program.<p>
"I was standing in the crowd of protesters, and the police grabbed me and threw me down on the gravel, rubbed my face in the gravel, causing wounds I still have on my face," said Cuddeford. "While being under a pile of about four police officers, they began applying their taser to me in my back, ordering me to put my arms behind my back.<p>
"My arms were pinned under my own body. I could not move them. They tasered me three times while I was down on the ground and then dragged me across the pavement and charged me with third-degree assault."<p>
More than 25 people were arrested in the week of demonstrations. But protesters have vowed not to be deterred in their effort to oppose the militarization of the ports.<p>
"Two to three more shipments of Stryker tanks will be sent to Iraq in the coming months," said Carrie Hathorn, of the Seattle-based Troops Home Now Coalition. "We will be back at the Port of Tacoma, and this time with many more people."<p>
Tom McCarthy of Tacoma PMR summed up the significance of the Port Militarization Resistance movement. "Through our defiance of the shipment of the striker tanks and the escalation of this war, we have shown that you can resist this war and have helped to make the antiwar majority visible."<p>
-- At the <b>University of Massachusetts-Amherst, </b>about 50 people took part in a spontaneous counterprotest on March 8 in response to a pro-war rally sponsored by the College Republicans.<p>
"Islam is more dangerous than Communism because at least Communism was a product of Western civilization," was the message of the main speaker at the so-called "Support Our Troops" rally.<p>
This hate speech contributes to a volatile atmosphere in which a student was a victim of a hate crime a few short weeks ago. We need to continue to stand up to these bigots.<p>
-- In <b>San Francisco, </b>40 people came out to a meeting to support military resister Travis Murphy, a member of Iraq Veterans Against the War (IVAW) on March 10.<p>
Murphy recounted the evening of September 11, 2001, while stationed in Germany. "I wondered who we were at war with as I wandered around a housing complex with an automatic rifle in my 19-year-old hands," he said.<p>
Murphy described his experience in Iraq and how he became antiwar. He described an experience with a young Iraqi man in a market, who he kicked to the ground when he put a hand on the soldier's weapon. "I looked at this man I had just kicked," he said. "He had no weapon. He wasn't the enemy. He just wanted me to stop aiming my weapon at people doing their shopping."<p>
The meeting was followed by sign-making for a March 18 antiwar protest. "What can we actually do for Iraq?" asked panelist Elizabeth Terzakis of the International Socialist Organization. "Give them money and leave them alone," she said.<p>
<i>Charles Peterson and Somerset Stevens contributed to this report</i>.<p>

