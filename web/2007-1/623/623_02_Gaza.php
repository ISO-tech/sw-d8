<font face="Times New Roman, Times, serif" size="5"><b>Nightmare at Gaza border</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 16, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">"IT'S LIKE the end of the world." That's how Abdel Hadi Salama described the scene at the Rafah border crossing in Gaza to an Associated Press reporter.<p>
Seven Palestinians were injured March 8 when more than 5,000 people gathered at the only border crossing out of Gaza into Egypt for a chance of getting out. A 61-year-old man on his way to visit a doctor in Egypt died of a heart attack.<p>
The terminal in Rafah opens only sporadically, and thousands amassed overnight in anticipation of a possible opening that morning.<p>
Salama, who was traveling to Egypt to visit relatives, told reporters via cell phone that when Palestinian police lost control of the surging crowd, people began throwing stones, and security personnel started firing their weapons. Press reports said two of the injured had been shot.<p>
According to a 2005 agreement, the Rafah terminal is supposed to be controlled by Palestinians and Egypt, along with European monitors. Israel, however, has the final word on whether it opens.<p>
Between June 26, the beginning of "Operation Summer Rain," Israel's incursion into Gaza, and the end of last year, the Rafah point was open only 14 percent of days, according to a March 12 report from the Al Mezan Center for Human Rights. And during the month of January, the Rafah crossing didn't open for a single full day.<p>
"When open, the hours have been limited, and perhaps worse, the opening of the terminal has only been announced a few hours in advance, preventing people from making plans, and leaving Palestinians fearing to use the crossing should they not find it open again for several weeks," the Al Mezan report states.<p>
Thus, at any give point, thousands of people are likely to be either trapped in Gaza trying to leave or trapped in Egypt waiting to return.<p>
The situation in Rafah is one devastating scene among many that illustrates Israel's ongoing attempt to starve the Palestinian people into submission. As a result of the U.S. and Israeli blockade of the Palestinian economy after the election of Hamas to lead the government last year, millions of Palestinians suffer without many of the most basic necessities.<p>
According to a new report by the World Food Program (WFP), the problem is most acute in Gaza, where clampdowns at border crossings have pushed unemployment to 39 percent and exposed 54 percent of 1.4 million Gazans to "food insecurity." Workers who in the past could count on being able to feed themselves and their families, such as fishermen and farmers, are now being counted among the hungry, says the WFP report.<p>

