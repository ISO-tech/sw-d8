<font face="Times New Roman, Times, serif" size="4">Exonerated death row prisoners testify in Maryland</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Proof the system is broken</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 16, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">MANY HAVE called Maryland's death penalty a "failing system." Recently, two committees in the Maryland House and Senate heard wide-ranging testimony for and against repeal, while a moratorium on executions has been in place since December.<p>
Legislators could decide either to fix the procedural flaws that precipitated the moratorium, maintain the moratorium or abolish the death penalty altogether. Democratic Gov. Martin O'Malley has come out in support of repeal. Reading from an op-ed he published in the <i>Washington Post, </i>O'Malley testified that the death penalty risks killing innocent people, has no deterrent effect and runs contrary to notions of essential human dignity.<p>
A 2002 University of Maryland study showing racial and geographic disparities in Maryland capital cases led to a moratorium that was reversed in 2004 by incoming Republican Gov. Robert Ehrlich Jr.<p>
Though most people testifying before the committees focused on innocence, deterrence and the cost of capital punishment, Katy O'Donnell, chief attorney for the Maryland office of the Public Defender, was one of the few to argue that race tends to determine sentencing, showing the continued urgency of the study's results.<p>
Numerous studies across the country have found similar disparities--in particular, prosecutors are much more likely to seek the death penalty in cases involving a Black suspect and a white murder victim. In a dozen states, moratoriums are in place, with Maryland's following directly on the heels of California and Florida, where a court and Gov. Jeb Bush halted executions over concerns with lethal injection.<p>
Before the hearings started, Maryland Citizens Against State Executions held a press conference with several prominent death row exonerees, including Kirk Bloodsworth, Ray Crone, Shujaa Graham and Shabakka Kwalevy.<p>
Asked how an innocent person could be convicted, the exonerees cited a litany of problems, including prosecutorial misconduct, falsified evidence, an inadequate defense and the political motivations of prosecutors and judges.<p>
Some of the most powerful testimony at the hearings came from these exonerees. "I am living proof that our capital punishment system is broken," said Kirk Bloodsworth, who in 1993 was the first in the nation to be released on DNA evidence, provided by the Innocence Project.<p>
One Senate committee member, Republican Bryan Simonaire, who supports capital punishment, asked, "Who's standing up for the victims of these heinous crimes?" He was strongly rebuked by two victims' family members, Bonita Spikes and Vicki Schieber. Said Schieber, "The death sentencing system in this state has failed victims' families."<p>
Bonita Spikes has been an outspoken critic of the death penalty ever since her husband was killed in 1994. She said the said the money used for capital cases could have helped her son, who tried to commit suicide in the wake of his father's murder, and has been emotionally unstable ever since. "A life is a life," she said. "I'm sure all these brilliant people working here" can come up with a suitable alternative to capital punishment.<p>
In response to the hearings, John Booth-El, a Maryland death row inmate for 24 years, said the death penalty has a dehumanizing effect, since it's based on the assumption that a criminal must always be a criminal. "If an inmate was pardoned based on his rehabilitating himself, it would expose the entire fallacy of the death penalty," he said. He echoed the words of Kirk Bloodsworth, who had testified that his case opened up a "Pandora's box" for capital punishment.<p>
Booth-El and fellow inmate Vernon Evans are scheduled to call in to at least two "Live from Death Row" events planned by the Washington, D.C., Campaign to End the Death Penalty.<p>
Though recent news reports have described the bills before Maryland's House and Senate committees as "faltering," Maryland residents are 63 percent in favor of repealing the death penalty, given life without parole as an alternative. The voices of death row inmates could vivify that majority sentiment, channeling it into pressure on legislators who otherwise might let repeal die while the moratorium stands.<p>
Apart from petitioning and letter-writing, a demonstration called in Baltimore on March 24 outside of the Maryland Correctional Adjustment Center, better known as Supermax, where Maryland's death row is located, will be the next chance for Maryland's death penalty opponents to voice their position: Abolish the death penalty in Maryland!<br>
<b>Chris Yarrison and Kevin Boston</b>, Baltimore<p>

