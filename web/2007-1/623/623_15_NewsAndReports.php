<a name="Lynne"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend Lynne Stewart</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Steve Leigh</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--Radical lawyer Lynne Stewart spoke to a crowd of 100 people here March 10 in commemoration of International Women's Day.<p>
Her speech in Seattle was part of a 15-city tour to encourage radical activism and keep up public support around her case. The meeting was sponsored by Radical Women, the Arab American Community Coalition and the Black Panther Party Reunion Committee, among others.<p>
Stewart is facing a 28-month federal prison sentence after being convicted in 2005 on charges including "materially aiding" terrorism, stemming from her issuing of press release from her imprisoned client, Sheik Omar Abdel-Rahman. Though no violence ever resulted from her actions, the government sought a 30-year prison sentence for Stewart, who is well known for her decades-long legal career defending radicals and the poor.<p>
Popular support for Stewart helped force the judge to impose a shorter sentence of 28 months. Now, the government is appealing to overturn what it sees as a "too short" sentence. Lynne also is appealing her conviction.<p>
Stewart began by expressing admiration for her heroines--including Harriett Tubman, Mother Jones and Rosa Luxemburg. She denounced the war on Iraq for oil and empire, the racism of the criminal injustice system, and called for the dismantling of the "prison-industrial complex." She also spoke against the "war on terror" and the current attacks on immigrants.<p>
Stewart ended her talk by calling on people to build a third party, since both Republicans and Democrats are pro-business and pro-war. "Agitate! Keep the groundswell going," she told the crowd.<p>
Lynne ended by quoting Indian anti-globalization activist Arundhati Roy: "A new world is not only possible--on a quiet day, I can hear her breathing!"<p>
<i>Contributions to the Lynne Stewart Defense Fund can be made out to the National Lawyers Guild Foundation (please note "For the Lynne Stewart Defense Fund"), and sent to the Lynne Stewart Defense Committee, 350 Broadway, Suite 700, NY, NY 10013</i>.<p>
