<p><a name="SF8"></a><font face="Times New Roman, Times, serif" size="4"><b>Justice for the San Francisco Eight</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Roberto Rosario</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--A diverse crowd of more than 200 people came together recently at a meeting to express their solidarity for eight former Black Panthers who were recently arrested on trumped-up murder charges stemming from the 1971 killing of a white police officer.<p>
The event was titled "Whitewashing the Panthers: Can the Government Prosecute Black History?" and was sponsored by the Malcolm X Grassroots Movement, the New York City Jericho Movement, the New York City chapter of the National Lawyers Guild and the Center for Constitutional Rights.<p>
Three of the defendants were tried in 1975. At the time, the charges were dismissed when it was revealed that New Orleans police had tortured the men into giving false confessions. Now, charges are being dug up again, and the defendants--dubbed the "San Francisco Eight"--need support.<p>
Bob Boyle of the National Lawyers Guild spoke of the need to free all political prisoners. Daniel Bell, the son of one of the San Francisco Eight, echoed that sentiment, telling the crowd that "Political prisoners exist and they need to be free."<p>
Panelists also made the connection between the war on our rights at home and abroad. Harold Taylor, one of the San Francisco Eight, said "The same thing is happening all over the world in Guant&aacute;namo, Iraq, Afghanistan." Gitanjali Gutierrez, a lawyer for one of the prisoners at Guant&aacute;namo Bay, Cuba, called for solidarity with the Muslim population that are being tortured and forced to confess--the same tactics used to frame Black activists here at home.<p>
