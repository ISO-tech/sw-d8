<p><a name="Immokalee"></a><font face="Times New Roman, Times, serif" size="4"><b>Support the Immokalee workers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Nadia Sol Ireri Unzueta Carrasco and Orlando Sep&uacute;lveda</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AS PART of the Campaign for Fair Food, the Coalition of Immokalee Workers (CIW) is preparing for major mobilizations for farmworker justice in Chicago in April. In mid-February, 70 young activists from around the Midwest attended a conference to discuss strategies to build the April mobilization.<p>
Nearly two years after a successful campaign forced Taco Bell to increase the price it pays for the tomatoes it uses in its restaurants, and pass on the difference to the tomato pickers, the CIW is now after fast-food giant McDonald's.<p>
McDonald's is behind the creation of Socially Accountable Farm Employers, which is supposed to monitor industry employers but has been denounced by consumer, human rights and labor organizations for covering up abuses in the fields. The conditions faced by tomato pickers, who are mostly immigrant workers, amount to modern-day slavery.<p>
Cruz Salucio, who has worked two years in the field, told conference attendees how 12 workers had to share a small trailer. "Then," he said, "they take $70 for that housing arrangement."<p>
Part of the conference was dedicated to discussing solidarity, particularly with the workers in Immokalee, Fla. Immokalee workers spoke about the links between free trade and globalization and their exploitation.<p>
"In Oaxaca, my family had a coffee farm," said Norberto Jim&eacute;nez, "but with the NAFTA the price of the coffee plummeted, and then the farm couldn't sustain us any more. So I decided to come here. "But I had to try more than once because the migra stopped me the first time.<p>
In the afternoon, 40 activists staged a protest at a local McDonald's, chanting "Down, down with exploitation! Up, up with a fair food nation!"<p>
The CIW is planning a rally outside McDonald's headquarters in Oak Brook, Ill., for April 13 and a "Carnival and Parade for Fair Food, Real Rights, and Dignity" for April 14 in downtown Chicago.<p>
