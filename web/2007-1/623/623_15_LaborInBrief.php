<a name="Stop"></a><font face="Times New Roman, Times, serif" size="4"><b>Stop &amp; Shop</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Wayne Standley</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THE FIVE locals of the United Food and Commercial Workers (UFCW) union, representing 43,000 Stop &amp; Shop workers in southern New England, voted to accept a new three-year contract March 11.<p>
This came after three months of negotiations and an overwhelming vote by the workers to authorize a strike last month. Immediately after the agreement, Stop &amp; Shop issued a press statement saying it believes "our new contracts meet our goal."<p>
In 2004, the union accepted a contract containing concessions in benefits on health care for part-time workers. This time, the company wanted even more, and was prepared to try to bust the union if it didn't get what it wanted.<p>
In preparation for a strike, the company opened several scab hiring halls. Full-page newspaper ads were also used to try to convince customers to continue to shop there in event of a strike.<p>
But public support was with the workers. A survey on the Web site of local radio station WHAI showed more than 80 percent would not cross a picket line.<p>
The workforce at Stop &amp; Shop is increasingly divided among full-time workers, part-timers who would like to be full-time, and a large group of part-timers who see no future in working for the company, as promotion opportunities are limited. The company increasingly has shifted towards this last group of workers--who now make up 80 percent of the company's workforce--because they receive few benefits.<p>
Unfortunately, UFCW has been willing to trade concessions for one group of workers in order to hold onto benefits for another.<p>
This strategy of trade-offs in benefits seems to have continued with the current contract. Full-time workers will now have to pay a portion of their health care premiums, with the largest burden falling on those with families.<p>
For new part-time workers, the waiting period for health care benefits has been reduced from the ridiculous two years to a slightly less ridiculous one year. However, all current part-time workers must still wait two years to qualify for company health coverage.<p>
This effectively will deny most part-timers any health care coverage at all, since the annual turnover rate among part-timers is nearly 100 percent. New pay raises, calculated on an hourly basis, are also lower for part-timers.<p>
Such divisions among Stop &amp; Shop's workforce only weakens the ability to build a united fight for decent wages and benefits--at Stop &amp; Shop and throughout the entire retail food industry.<p>
The next struggle is already underway in Southern California, where the UFCW union members are negotiating to win back benefits lost in their last contract. The lessons of New England Stop &amp; Shop workers will be critical to a victory there.<p>
