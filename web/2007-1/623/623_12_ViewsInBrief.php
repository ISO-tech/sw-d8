<p><a name="Anarchism"></a><font face="Times New Roman, Times, serif" size="4"><b>The real debate on anarchism</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I THINK in Paul D'Amato's "Are anti-authoritarians the real left?" (February 9) and "Is all organization authoritarian?" (March 9), anarchists have been propped as straw men. This has done a disservice to the debate, and has undermined our ability to win over those attracted to what the anarchists have to offer (though anarchists continue to mischaracterize the term "vanguard" as well).<p>
Where I think Paul errs is when he equates the anti-authoritarianism of the anarchists with the principled pacifists we see in the antiwar movement. There aren't any serious anarchists who are blanket "anti-authoritarians." No serious anarchist would argue against the authority of a strike. No serious anarchist would argue against "submitting" to the authority of traffic lights.<p>
Engels' "On Authority" is also weak, for the crux of his argument is that a revolution is the most authoritarian thing there is. Yet there is no contradiction in an anarchist arguing for revolution and being anti-authoritarian, for it isn't authoritarian to destroy the authority of a minority class.<p>
The real root of the debate, I believe, is that anarchists and Marxists have entirely different views on what the state actually is. Does it serve the function of existing "over society"--only allowing for minority control--or is it a historical mediator of class conflict?<p>
Also, whether if in the process of revolution new organs of society can emerge to replace the state, or if a new society, as Marx put it, is "not as it has developed on its own foundations, but, on the contrary, just as it emerges from capitalist society; which is thus in every respect, economically, morally and intellectually, still stamped with the birth marks of the old society from whose womb it emerges."<br>
<b>Alex Fu</b>, San Francisco<p>

