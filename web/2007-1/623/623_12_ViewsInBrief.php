<p><a name="Blair"></a><font face="Times New Roman, Times, serif" size="4"><b>Tony Blair's blood and lies</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THE RECENT comment by British Prime Minister Tony Blair that he feels anguish at the Iraqi death toll, but that it's not the Coalition's fault, is pure delusional fantasy.Yes it's true that the bulk of the killing is as a result of the insurgency, but the insurgency is a direct result of he and his master--George Bush--illegally invading a sovereign nation and causing the insurgency to happen.<p>
You are guilty, Prime Minister. Guilty of mass murder, willful infanticide, mass destruction, fear and sectarian violence.<p>
Your legacy to history will be the blood of innocents that you have shed in our name, using a deliberate lie. You don't need to worry about global warming--it's a lot hotter where you'll end up.<br>
<b>Ad Williams</b>, Anglesey, United Kingdom<p>
