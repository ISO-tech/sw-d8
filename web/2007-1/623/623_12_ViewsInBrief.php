<a name="Recruit"></a><font face="Times New Roman, Times, serif" size="4"><b>Keep recruiters off our campus</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I AM a student and activist at City College of New York, where there will be a career fair, with all of the branches of the military represented. They will be preying on the most vulnerable.<p>
I almost joined in 2004, and my sister did serve 10 years in the military--because we both couldn't afford college tuition. She fought in both Afghanistan and in Iraq, and was fortunate enough to come back in one piece. When she did come back, she said that she thought that Bush was a liar.<p>
From experience, I know some of the deadly lies recruiters tell and how they pretend to be your best friend. Knowing this, I recently went to the career center to ask if there was some way I could get a table during the event. I told them that I would like to inform students of the risk of joining the military.<p>
Not only that, but the military is a discriminatory institution that prohibits gays from serving openly. This is a direct violation of the school's anti-discriminatory policy.<p>
Eventually, I was denied a table because they said I wasn't offering a job, nor did they have space. They said that the career fair was not the forum for me to express my opposition, and to basically take it somewhere else.<p>
I went to the anti-discrimination office on my campus to inquire about the antigay policies of the military and the violation their presence on campus constitutes. The director said that the military was waived from that policy because of a Supreme Court ruling, and that neither he nor I could do anything. Where is the democracy in all of this?<p>
However, on March 8, students will voice their opposition to the presence of the military at our school. This whole situation reminded me of a chant I heard during an antiwar protest that goes: (pointing at protesters) "This is what democracy looks like," and (pointing at the school) "That is what hypocrisy looks like."<br>
<b>Roberto Rosario</b>, New York City<p>
