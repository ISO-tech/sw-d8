<font face="Times New Roman, Times, serif" size="4">Immigrant victims of deadly NYC fire</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>This tragedy didn't have to happen</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jake Kornegay</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 16, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">A FIRE that swept through an overcrowded building in New York City last week killed 10 people from two African immigrant families--nine of them children, ranging in ages from 7 months to 11 years old.<p>
Thirteen other relatives were injured, suffering burns or broken bones from jumping out windows--and a number of firefighters were hurt. The fire was the deadliest in New York in close to two decades--since the blaze at the Happy Land nightclub in 1990 that killed 87 people.<p>
The building--in the Highbridge neighborhood of the Bronx, a center for immigrants from Mali--is nothing but a smoldering shell now, as New Yorkers come from all over the city to pay their respects, and consider whether such an awful tragedy could also happen to them.<p>
All of the unsafe conditions that contributed to the disaster--the all-wooden stairs leading up a flue-like hallway to the top floor, the lack of sprinklers and fire escapes--are legal under New York City building codes. And the city's affordable housing crisis, which led the 22 Malian immigrants to jam into the building, guaranteed a higher death toll.<p>
A faulty space heater cord is being blamed for starting the fire. But tens of thousands of New Yorkers turn to space heaters as a last resort each year when the temperature drops in their apartments due to inadequate heat. According to the U.S. Consumer Product Safety Commission, space heaters cause over 25,000 fires and 300 deaths across the country each year.<p>

