<font face="Times New Roman, Times, serif" size="3">And what was the response of New York City's billionaire Mayor Michael Bloomberg? Distribute batteries. "It's impossible to say whether this would have saved lives, but smoke detectors really are the best defense that we have against fires, and not using them is a risk that none of us can afford to take," Bloomberg said.<p>
But there are much better "defenses" against these tragedies--if only Bloomberg and city officials would spend the money.<p>
The Department of Buildings said a work permit for the building, stalled on technicalities, would have allowed renovations, including a new sprinkler system, fireproof metal stairs and a fire escape. Meanwhile, more than $400 million of taxpayer money that could be used for safe, affordable housing is going instead to help build a new Yankee Stadium, just blocks away from the site of the Bronx fire.<p>
The crisis of affordable housing highlights the holes in the U.S. economy for the nation's poor.<p>
In the past year, the number of homeless families in New York City increased by 17.6 percent to an all-time high in February 2007, with the Department of Homeless Services reporting a monthly average of 9,287 families in the city's shelters. "This is unfortunately a pretty straightforward story," said Brad Lander, Director of the Pratt Center for Community Development. "Housing costs continue to skyrocket while wages on the low-end can't even begin to catch up."<p>
Under the city's housing program for the poor, 40 percent of all families receiving housing placement in the Bronx were placed in buildings with more than two hazardous violations per apartment--including lead paint, broken windows, collapsed ceilings and floors, non-working appliances and fixtures, as well as a lack of heat, hot water and electricity. New York City law defines each of these violations as "immediately hazardous" to the health and safety of tenants living in the apartment.<p>
Under these circumstances, it is inevitable that poor families would crowd into any available housing, no matter how dangerous the conditions. Plywood or sheets are commonly used as room dividers in rooming houses, and people living in closets, under stairwells or in attics is increasingly common, according to Westbury, N.Y., Fire Chief Joseph Pascarella.<p>
Local residents, mosques, a nearby school and people from far and wide have shown their solidarity with the victims of the Bronx fire, raising money and gathering goods for the families--everything from blankets and food, to toys and stuffed animals for the surviving children.<p>
We should demand that the politicians show the same concern--and do something about the causes of terrible tragedies like this one.<p>

