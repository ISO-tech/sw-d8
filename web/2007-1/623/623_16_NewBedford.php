<font face="Times New Roman, Times, serif" size="3">Those who couldn't immediately prove their citizenship were rounded up and shipped to various prisons around New England, and even as far away as New Mexico--leaving scores of children stranded at school or at home.<p>
"They [ICE agents] were insisting sign here, sign here," Norma Urbina, one of the women rounded up, told the <i>Boston Globe</i>. "We didn't even know what we were signing. There were people who signed deportation orders because they couldn't read."<p>
Public shock at the havoc wreaked by the raid was immediate. The <i>Boston Globe</i> described the whole fiasco as a "human crisis" and Massachusetts Gov. Deval Patrick went to New Bedford to extend his sympathies.<p>
To add insult to injury, while three top managers at Michael Bianco were arrested at the time of the raid, they were quickly released--and back running the plant the next day. Meanwhile, hundreds of immigrants who labored under there are not so fortunate--they continue to languish in prison for the "crime" of wanting to make a good life for their families.<p>
The response from immigrant rights organizations and activists has been varied. Some have turned to relief-oriented work in a local church in New Bedford, providing services to family members of the detained. Others, including some of the bigger immigrant rights organizations in Massachusetts, remained focused on lobbying in Washington, D.C., hoping to achieve immigration reform legislation.<p>
Still others are looking to organize mass actions and protests in response to this injustice. In nearby Boston and Providence, activists are planning a series of pickets and protests that will target ICE offices.<p>
"We need to protest, scream, organize," says Guadalupe Hernandez, one of the activists organizing the ICE protest in Boston. "If we just sit and wait to see what happens by lobbying the politicians for immigration reform, there's just going to be more raids. We have to organize--that's the only thing we can do to stop this."<p>

