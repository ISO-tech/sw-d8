<font face="Times New Roman, Times, serif" size="5"><b>Rounded up and "disappeared"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Keith Rosenthal and Brian Chidester</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 16, 2007 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">A 7-year-old child calls up a state hotline looking for her mother, and learns she was shipped to a detention center in Texas. Frantic parents search for five children who were arrested and sent to a prison in Miami in the middle of the night. Hundreds more are detained and "disappeared" within 48 hours, to places far from their home.<p>
These are the cruel consequences of a March 6 raid by Immigration and Customs Enforcement (ICE) agents on a sweatshop in New Bedford, Mass. Three hundred and sixty-one workers, mostly Guatemalan women, were captured in the sweep.<p>
The raid occurred at the Michael Bianco Inc., textile plant, which has a $91 million contract with the U.S. Army to produce protective gear for U.S. troops.<p>
Conditions for the mostly immigrant workforce were deplorable. Workers were paid an average of $7 an hour, were docked $15 for every minute they were late to work, and were given $20 fines for spending more than 2 minutes in the bathroom or talking while working.<p>
The March 6 raid was a scene of terror. With 300 federal agents storming into the plant, workers frantically attempted to escape, only to be turned back by the freezing cold outside. Many were trampled, and several had to be hospitalized.<p>

