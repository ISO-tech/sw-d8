<font face="Times New Roman, Times, serif" size="5"><b>Marines out to silence their own</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 1, 2007 | Page 1</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>ERIC RUDER reports on the Marine Corps' threats against two antiwar veterans.</b><p>

THE U.S. Marine Corps is trying a new approach to silencing criticism from veterans--discharge them again, but this time less than honorably.<p>
Former Marines Liam Madden and Adam Kokesh served in Iraq and were both honorably discharged. Upon their return, they both joined Iraq Veterans Against the War (IVAW).<p>
Liam is also a cofounder of the Appeal for Redress, a statement signed by nearly 2,000 active-duty troops that garnered a lot of media coverage, and he's currently helping to organize a bus tour to some 20 military bases throughout the eastern U.S. Adam is in Germany, reaching out to soldiers at the U.S. Army base at Ansbach.<p>
Both Adam and Liam participated in the first installment of Operation First Casualty, in which IVAW members brought the war home through street theater by mounting "patrols" in the streets of Washington, D.C., on March 19.<p>
Now the Marine Corps is trying to make an example of them. Both men are part of the Individual Ready Reserve (IRR), an inactive status that practically all troops pass through prior to final separation from the military.<p>
Only once--in July 2004--have troops on IRR been recalled to active duty, and more than one-third of the 5,000 soldiers summoned by the Army failed to appear. None faced any kind of sanction--much less a change in their discharge status to other than honorable.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">THE MARINES claim that Liam and Adam appeared in uniform at a political event, which is a violation of military rules that give troops the right to participate in political action so long as they do so off-base, out of uniform and while off duty.<p>
Regardless of the truth of this allegation, the Marine Corps is taking extraordinary steps in trying to subject two Marines on IRR to such regulations. "These are important issues, and they go to the question of military-civilian balance, and when you cease being bound by military rules," said Tod Ensign, director of the New York City-based Citizen Soldier, in an interview. "Are Liam and Adam bound by those rules? I'd say hell no."<p>
"This is just a trial balloon, and it's harassment," Ensign added. "But if they get away with it, you can be sure that they will then start becoming more draconian, and their sweep of other people will be expanded. This could have a very chilling effect on the IVAW, to say the least."<p>
For his part, Liam understands the stakes--and is making plans for responding.<p>
"The military is trying to stifle the IVAW's voice in the public discourse about the war," Liam said. "They perceive me as a figurehead or a vocal component of this, and it's their tendency to nip that in the bud by going after the figurehead. The troops are allegedly fighting for freedom and democracy, yet their voices are suppressed when they get home--if their voices go against the grain.<p>
"I'm sure that when the former commandant of the Marine Corps was wearing his uniform the other day at a policy meeting, they would never dare to downgrade his discharge for wearing a uniform at a political event. But when Adam Kokesh does it during Operation First Casualty, it's a different story. I don't think they'll get away with this. It's pretty groundless."<p>
Paul Abernathy, an IVAW member from Pittsburgh, agrees that the issue is larger than just Liam and Adam.<p>
"The reality is that the military is concerned because veterans from all across the country are becoming part of a national movement to resist this war," he said in an interview at an IVAW Memorial Day event at the Vietnam War Memorial in Chicago. "They recognize this, they're afraid of it, and so they're trying to set an example to discourage future veterans from coming forward.<p>
"In Iraq, the only concern that soldiers have is spending 365 days and coming home alive. No one is trying to institute 'democracy' or social transformation there, and no one is battling terrorists, because that's not the reality.<p>
"The reality is that soldiers are put in a difficult position--to enforce an unjust and futile policy. Everyone knows it, and no one wants to lose their life for it. It's difficult to take the next step to come out and speak out publicly with the IVAW, but at every event, more veterans are standing with us."<p>

