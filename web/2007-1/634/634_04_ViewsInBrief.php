<p><a name="LAPD"></a><font face="Times New Roman, Times, serif" size="4"><b>Standing up to the LAPD</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I WOULD like to thank <i>Socialist Worker</i> for its coverage of the brutal attacks by the thugs in the LAPD on the 15,000 participants of a May Day rally for immigrant rights in historic MacArthur Park in Los Angeles ("Outrage at LA cops' May Day assault," May 11).<p>
I was one of the millions across the U.S. who were outraged when they watched on TV how riot cops tear-gassed, clubbed and fired rubber bullets at the crowd and at reporters covering the demonstration.<p>
The LAPD has a long record of brutality, exemplified through the 1992 beating of Rodney King. The LA riot was a product of that, but it was also a product of the mass class and racial anger that existed in black and Latino communities.<p>
Police Commission President John Mack was quoted saying that the attacks on immigrants will "reopen old wounds, regrettably." "Regrettably," in the sense that it would really hurt the LAPD and the government if victims of police repression and immigrant workers united to denounce police brutality and fight for immigrant rights.<p>
The anger that exists in society towards the war on Iraq, the raids and deportation of undocumented workers and the lack of health care are having an impact on mainstream politics, shifting it to the left. This welcome shift is being driven by the rising inequality between workers and bosses. In turn, this increases the volatility of this period, where, for example, a pro-immigrant rally on May 1 can turn into a movement that involves groups and organizations fighting against police brutality and for worker's rights.<p>
It is important that the May 17 rally against police brutality brought out 10,000 people to demand justice and civil rights for all immigrants and to declare "our voices will not be silenced!" This could be the beginning of an important step of linking the immigrant right's movement to the broader attacks against working people of this country.<p>
We should demand the jailing of the cops who attacked the peaceful protesters. Both the government and the bosses enjoy the role that the police play in keeping immigrants underground and frightened and we should jump on the opportunity to begin turning the tables around. <br>
<b>Alvaro Lopez</b>, New York City<p>
