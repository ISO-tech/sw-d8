<a name="Lazy"></a><font face="Times New Roman, Times, serif" size="4"><b>Our right to be lazy</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THANK YOU so much for Paul D'Amato's article "The economics of laziness" (May 4). I read it at work, while my partner was asleep in our van. Later after he read it, we talked about how much more work we do to make someone else rich with all the new technology.<p>
If it's shocking that we have that much freedom on the job to bullshit and rest, I think it's something more people should have, given how stressful and crazy work is these days.<p>
In our job, we are outside in the freezing cold, the blistering heat, rain, snow, humidity and everything else. Most of us wind up with chronic pain from working in cramped spaces and with repetitive motion, or serious falls. We work on roofs and underground and up poles. We get stung by bees, chased by dogs and verbally, and sometimes sexually, harassed by customers. We've been robbed and beat up and stabbed.<p>
Yet the bosses think they take the risk by investing "their" money, so they deserve the bigger piece of the pie! It makes no sense to work harder just to put money in their pockets.<p>
Thanks again for always putting the perspective of working people right at the heart of your paper. We love reading it every week.<br>
<b>Anonymous, </b>from the Internet<p>
