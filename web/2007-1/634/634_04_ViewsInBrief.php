<p><a name="Defeat"></a><font face="Times New Roman, Times, serif" size="4"><b>Heading for an imperial defeat?</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THANKS FOR the excellent editorial, "Heads buried in the sand" (May 11). There is one part of the article that is extremely interesting: "But instead of showing off Washington's overwhelming strength, the war has exposed its weakness. The best-armed war machine in the history of the world has been unable to defeat a poorly equipped guerrilla force."<p>
Is history repeating itself? Back in the latter part of the 18th century, the world's mightiest war machine was Great Britain. When the "insurgents" in the colonies tired of that occupation, they banded together in a form of guerrilla warfare that not only kept the Brits off balance, but managed to send Britain home, defeated by a rag-tag army of farmers and shopkeepers. Sounds familiar, doesn't it?<p>
I agree that both Bush and his enabler, Cheney, have their heads buried--but not in sand. No, these two have chosen to bury their heads closer to home. Anatomically speaking, they are both suffering from the classic "cranial-rectum" inversion.<br>
<b>Greg Bacon</b>, Ava, Mo.<p>

