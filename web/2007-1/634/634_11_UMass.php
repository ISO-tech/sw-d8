<font face="Times New Roman, Times, serif" size="5"><b>UMass students say: "Dis-Card!"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 1, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>JAMES FIORENTINO reports on the protest against former Bush administration official Andrew Card.</b><p>

AMHERST, Mass.--Hundreds gathered at the University of Massachusetts' (UMass) graduate commencement ceremony to protest the awarding of an honorary degree to former White House Chief of Staff Andrew Card on May 25. Card played a key role in shaping the Bush administration's case for war against Iraq--a case built on lies and deceit.<p>
Outrageously, UMass administrators chose to give Card an honorary doctorate of public service, citing his "high ethical standards." The large crowd that gathered outside of the Mullins Center, where the ceremony was to take place, had a different assessment.<p>
"After two tours in Iraq, where I saw what the policies of the Bush administration meant for ordinary Iraqis, I have decided to get political," said Nathan Murphy, a Marine veteran and UMass undergraduate. "By honoring Andrew Card, UMass has brought shame upon itself. I'm here to show that veterans are antiwar, too."<p>
History professor Sigrid Schmalzer expressed the sentiments of UMass faculty who signed a petition calling for the university to revoke its offer to Card. "For the university to so cynically disregard the question of intellectual integrity when it becomes convenient to pursue money and power is the wrong message to send," she said, alluding to the charge that top university officials used the degree offer to repay Card for political favors while he was in the White House.<p>
Jeffery Dupry, 28, was forced to suspend his graduate studies to serve a tour in Iraq. "Soldiers are getting left behind by our government," said Dupry, who signed the Appeal for Redress that was circulated among active-duty troops in the Middle East, calling for the withdrawal of troops from Iraq.<p>
"Bush says 'Support the troops,' but the Department of Veterans Affairs doesn't care about us once we've returned home. Soldiers are not fighting for this government. We're only fighting for our survival in Iraq."<p>
Inside the arena, a majority of the crowd held signs that read "Honor Grads: Dis-Card!" Protest organizers encouraged families of graduating students to participate, and the response was overwhelmingly favorable. When provost Charlena Seymour announced Card's name, the arena erupted into thunderous boos and chants that lasted for four minutes.<p>
The faculty assembled on the stage behind the podium stood up and unfurled large banners that read "Card: No Honor, No Degree," and "War Criminals not Welcome: Card Go Home." The noise from the audience completely drowned out the provost and university president.<p>
Statements of solidarity from across the country are pouring into UMass protesters. The response indicate that people are generally fed up with endless war and the lies of the Bush administration. As the thousands of parents, students, faculty, staff and community members made clear on Friday, our voices will be heard.<p>

