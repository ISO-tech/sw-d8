<font face="Times New Roman, Times, serif" size="5"><b>How charter schools were used against LA teachers</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs, United Teachers Los Angeles</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 1, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES--Green Dot Public Schools announced May 9 that a majority of teachers at Locke High School in Watts had signed a petition supporting conversion of their struggling school into 10 small charter schools run by Green Dot.<p>
This surprise move, combined with the victory of two candidates supported by pro-charter Los Angeles Mayor Antonio Villaraigosa in the May 15 school board elections, has prompted the local ruling class to declare open season on United Teachers Los Angeles (UTLA), the union representing the 48,000 teachers, counselors, and health and human service professionals who work for the Los Angeles Unified School District (LAUSD).<p>
Green Dot CEO Steve Barr wants to rip up the UTLA contract. This would force all Locke teachers to re-apply for their jobs, eliminate retiree benefits, and impose several hours of extra, unpaid labor on teachers each week. In a local radio interview, Barr essentially admitted that Green Dot also plans to discriminate against veteran teachers (whose salaries and benefits the company supposedly cannot afford).<p>
Meanwhile, the May 17 lead editorial of the <i>Los Angeles Times, </i>titled "LAUSD's opportunity," urges the district to "take on the teachers' union," which the editors call "the most regressive force" in the LAUSD.<p>
The <i>Times</i> wants blood. "The board should be ambitious," the editors argue, "It needs more flexible work hours and duties--such as teachers supervising kids during lunch hours and after school to make campuses safer. It should push to soften tenure rules so that bad teachers are easier to fire and good teachers are easier to reward, and insist that students' needs, not teachers' seniority, guide job assignments."<p>
The reality is that public school teachers are overworked--which is why UTLA has fought for contract language freeing teachers from supervision duties during meal breaks and after school.<p>
If the LAUSD bureaucrats or the <i>Times</i> editors truly cared about "safer campuses," they would push for money to hire more campus supervision aides at our overcrowded schools. And attacks on teacher seniority are not about serving "student needs," but about giving principals power to squeeze out experienced teachers and replace them with new hires who are cheaper and easier to intimidate.<p>
The reality is that charters typically "cherry pick" their students. Green Dot schools have selective enrollment, much lower populations of special needs students and English learners, and get to kick out kids with discipline problems. When kids at mainstream public schools (kids that Green Dot doesn't want to teach) have lower test scores and graduation rates, then the charter school advocates gush over this "proof" that charters work.<p>
Charter schools claim to offer autonomy from school district bureaucracies. This was how Green Dot convinced many Locke teachers to sign the petition. Zeus Cubias, a math teacher at Locke, told the <i>Times </i>that many teachers who signed the charter petition remained dubious of Green Dot's plans, but "feel like there isn't any other option." The vote at Locke was thus motivated by teachers' desperation to escape the LAUSD bureaucracy.<p>
The reality is that LAUSD and Green Dot aim to discredit public schools, scapegoat teachers and their unions, and pave the way for the further privatization of education. The real alternative is to cut the bureaucracy, tax the rich, and put this money directly into the classroom, with teachers, parents and students having final say over how to spend it. But this is a solution that neither Green Dot, nor the LAUSD bureaucracy, nor the editors of the <i>Los Angeles Times</i> are offering.<p>
We're going to have to fight for it, and we're going to need UTLA more than ever to win.<p>

