<font face="Times New Roman, Times, serif" size="5"><b>The terrorist that the U.S. let walk</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 27, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">SOME TERRORISTS apparently aren't so bad in the eyes of the U.S. "justice" system.<p>
Last week, at the direction of a federal court, Luis Posada Carriles, an anti-Castro Cuban exile suspected of masterminding a string of terrorist bombings over several decades, was allowed to walk out of a U.S. prison on bail.<p>
Posada is notorious for his participation in right-wing plots since the 1960s, and declassified documents confirm that he was on the CIA payroll for much of the 1960s and early 1970s. In 1998, he bragged to the <i>New York Times</i> that he paid a Salvadoran mercenary to place a bomb in the Copacabana Hotel, in Havana, Cuba. The blast killed an Italian tourist.<p>
But Posada is probably most notorious for his suspected role--along with another Cuban exile, Orlando Bosch--in planning the October 1976 midair bombing of a Cuban commercial airliner that killed 73 people.<p>
Declassified CIA documents identified Posada and Bosch as the masterminds, citing a meeting between the two in Venezuela that occurred just before the bombing. Posada escaped from a Venezuelan prison in 1985, however, before a civilian trial could be completed.<p>
In 2005, after being pardoned in Panama for his part in a plot to assassinate Fidel Castro, Posada allegedly snuck into the U.S. illegally, and was arrested following an international outcry.<p>
But last September, U.S. Magistrate Judge Norbert Garney recommended releasing Posada, ruling that requests by both Cuba and Venezuela for his extradition cannot be honored because, Garney claimed, of the "probability" that Posada would be "tortured."<p>
Bush administration lawyers didn't challenge the unsubstantiated claim that either country practices "torture"--setting up the most recent ruling by district Judge Kathleen Cardone, upheld by the U.S. 5th Circuit Court of Appeals, which ordered the 79-year-old Posada released on $350,000 bond.<p>
The Justice Department has refused to classify Posada as a terrorist. Yet in court papers filed in his immigration fraud case, it described him as "an unrepentant criminal and admitted mastermind of terrorist plots."<p>
As Bernardo &Aacute;lvarez Herrera, Venezuela's ambassador to the U.S., wrote in the <i>New York Times</i>, charging Posada with immigration fraud instead of terrorism is "a travesty that could be equaled only by charging Osama bin Laden with entering and leaving Pakistan without a visa."<p>
U.S. Immigration and Customs Enforcement could step in to detain Posada until his trial, because the Cuban-born Venezuelan citizen faces a deportation order. But there's no sign that the Bush administration is willing to bring Posada to justice--because, among other things, this would risk exposing the extent that the U.S. aided Posada in his crimes.<p>
Posada's release on bail is a slap in the face to the families of his victims. As Livio di Celmo, whose brother Fabio was killed in the Copacabana bombing, told the <i>Miami Herald</i>, "Luis Posada Carriles is a terrorist, and the U.S. government has refused to define him as so. This [release on bond] is an insult to my brother and the other victims of terrorism."<p>
His release is also a sharp contrast to the fate of tens of thousands of undocumented immigrants held in prisons across the U.S.<p>
As <i>New York Daily News</i> columnist Albor Ruiz wrote, "While thousands of poor, decent, hardworking immigrants are kept detained in terrible conditions, Luis Posada Carriles, a dangerous man with a long criminal history and a pending deportation order, was freed...While hundreds of immigrant families are mercilessly torn apart by raids and deportations, this shady character was sent home to his wife and children."<p>

