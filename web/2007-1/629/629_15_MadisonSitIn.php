<font face="Times New Roman, Times, serif" size="3">However, he didn't address protesters' demands--which included the immediate withdrawal of all U.S. forces from Iraq; the full funding of veterans' benefits and health care, including mental health care; and reparations to the Iraqi people.<p>
In response, students decided to stay in his office until Kohl came to Wisconsin and held a public forum on the war. "This unremitting arrogance from the wealthiest man in the Senate has forced us to occupy this office in an act of civil disobedience," wrote Zach Heise in a press release circulated from Kohl's office during the action.<p>
Kohl's staff brought in private security guards to intimidate protesters and restrict their access to bathrooms, water and personal items. In addition, they quarantined people in a small conference room with sealed windows, and without enough room to lie down.<p>
Calls of solidarity came in throughout the evening. "They helped show people this is a bigger movement and we have support from all over the place," said CAN and IVAW member Todd Dennis. Twenty-five people decided to stay overnight.<p>
In the morning, protesters moved to take over the rest of the office. In response, Kohl's staff called in the Madison police to issue citations for "disorderly conduct" and drag out those who resisted. Students voted to stage an orderly retreat. "The protest showed Herb Kohl doesn't care what his constituents think," said Dennis.<p>
CAN members told reporters that the next step was to protest a Halliburton recruiting event on April 30.<p>

