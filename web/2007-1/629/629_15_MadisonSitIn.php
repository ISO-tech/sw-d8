<font face="Times New Roman, Times, serif" size="5"><b>Takeover of a senator's office in Wisconsin</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alex Stone</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 27, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">TWO HUNDRED University of Wisconsin-Madison students walked out of their classes on April 18 to demand an immediate withdrawal of troops in Iraq--and then occupied the Madison office of Wisconsin's Democratic Sen. Herb Kohl.<p>
The walkout was organized by the Campus Antiwar Network (CAN) and cosponsored by Iraq Veterans Against the War, the International Socialist Organization and al-Awda Wisconsin.<p>
"We protested Kohl because he postulates himself as an antiwar candidate, but he continues to vote for funding the war," said CAN member Kate Losey. "We want and demand something more from our politicians."<p>
At Kohl's office, CAN staged a teach-in featuring solidarity phone-ins from Howard Zinn, author of <i>A People's History of the United States, </i>and Dave Zirin, sportswriter and author of <i>What's My Name Fool?</i><p>
Eventually, the office director read protesters an official statement from Kohl. In it, Kohl professed solidarity with the students and said he would "join them in calling for a safe return home for our brave men and women serving in the region."<p>

