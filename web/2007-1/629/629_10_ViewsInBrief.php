<p><a name="Single"></a><font face="Times New Roman, Times, serif" size="4"><b>We need more than single-payer</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NO DOUBT a single-payer medical system would be an improvement over the current abysmal state of medical care in the U.S. ("Sick and getting sicker," April 13). However, as a Canadian doctor working in a single-payer system, I can testify that it does not guarantee access to medical care.<p>
I have a wait-list that is as large as my practice, and grows larger every day. Most of the general practitioners in my area have closed their practices to new patients, and a growing number of people have no family doctors at all.<p>
True, the Canadian style of rationing medical care is more fair than the American style of rationing. But why should we accept any form of rationing? Medicine should be freely available to all who need it. If capitalism cannot deliver that, then we need to create a society that can.<br>
<b>Susan Rosenthal, MD</b>, Toronto, Ontario<p>
