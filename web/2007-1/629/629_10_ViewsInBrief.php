<a name="Till"></a><font face="Times New Roman, Times, serif" size="4"><b>Fired for taking a stand for Till</b></font><p>

<font face="Times New Roman, Times, serif" size="3">TWO TEACHERS at a Los Angeles charter school got a lesson in just how little respect for-profit schools have for the teachers who make them run.<p>
Last month, Celerity Nascent Charter School seventh-grade teacher Marisol Alba and math teacher Sean Strauss were fired over an incident stemming from the planning of a February Black History Month celebration to honor the memory of Emmett Till, the 14-year-old who was lynched in 1955 after allegedly whistling at a white woman--and whose death helped spark the modern civil rights movement.<p>
Alba helped her students prepare the program, which included the reading of a poem about Till and plans to lay flowers in a circle. When school officials found out, however, they objected to the presentation, saying it was "inappropriate" for younger children<p>
According to Alba, the principal of the school also objected to what she termed Emmett Till's "sexual harassment" (Till had a speech impediment which sometimes caused him to make a whistling sound)--ridiculously comparing it to a construction worker whistling at a woman walking down the street.<p>
"She said that she would be offended by that, and that what Emmett Till did could be considered sexual harassment," Alba told the <i>Los Angeles Times</i> last month. "She used the phrase a couple of times, and when I objected, she said 'okay, inappropriately whistled at a woman.'"<p>
Several students defended Alba's account, and one parent who called in to complain said that principal Grace Canada shockingly described Emmett Till's supposed actions as "rude."<p>
Math teacher Sean Strauss was later fired for having signed onto one of more than 10 letters of protest circulated by students. The administration charged him with "disparaging the school to students and parents and authorizing by physical signature a nonsupportive message to the administrative staff."<p>
But as Strauss told the <i>Los Angeles Times</i>, "The kids felt strongly about this, and because these are my students, I felt one of my jobs was to pay attention to them."<p>
Eighty percent of Celerity's 500 students are African American, and 19 percent are Latino. Yet according to Celerity cofounder and Executive Director Vielka McFarlane, the Till celebration was inappropriate because "Our whole goal is how do we get these kids to not look at all of the bad things that could happen to them, and instead focus on the process of how do we become the next surgeon or the next politician."<p>
But not, presumably, one of the thousands of teachers working at the more than 600 charters schools in California--90 percent of whom, like Alba and Strauss, have no union protection and can be fired at any moment for standing up for what they believe.<p>
As parent Vera Hampton told the <i>Times</i>, "Those teachers should not have lost their jobs for standing up for what they felt was right; that sends the wrong message. The kids didn't even get a chance to say goodbye."<br>
<b>Diane Warren</b>, San Francisco<p>
