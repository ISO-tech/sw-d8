<p><a name="Recruiters"></a><font face="Times New Roman, Times, serif" size="4"><b>The pressure on recruiters</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AS AN ex-Navy-recruiter, I have to set the story straight. Everywhere I go, I see articles or news programs stating that military recruiters lie.<p>
For example, I just read an article by Chris White from CounterPunch that states, "Recruiters have monthly quotas and, once filled, they can slack off for the rest of the month. However, the more people they sign up, the better their chances for promotion. Therefore, the incentive for dishonesty is high indeed."<p>
This article makes me sick. There is no incentive on the part of recruiters themselves to be dishonest, none whatsoever. I was a Navy recruiter for three years and that was the worst three years of my life. The reason why military recruiters are dishonest is because of the pressure the upper chain of command puts on you. You have no idea of what I had to go through while I was at Navy Recruiting Command in Houston.<p>
For example, if a kid came in and told me that he or she smoked marijuana, my supervisor would tell me that I had three days to get the kid clean. If not, they would kick me out of the Navy for not obeying a order. I could go on and on how my chain of command made me do things that I did not want to.<p>
One of my dreams in life was to retire from the military. I was proud and honest, and I was blissful to serve my county. I recently separated from the military with honors. If I never had become a recruiter, I still would be in the military today. So where is the incentive to be dishonest? Trust me, it's not the recruiters!<br>
<b>Adam Mathews</b>, from the Internet<p>

