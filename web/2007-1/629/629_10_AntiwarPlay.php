<font face="Times New Roman, Times, serif" size="5"><b>Soldiers' voices banned from stage</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 27, 2007 | Page 10</font><p>

<font face="Times New Roman, Times, serif" size="3">THE PUBLIC high school in the little town of Wilton, Conn., has made it into the <i>New York Times</i> recently, but not for its high test scores or excellent academic performance record.<p>
Instead, principal Timothy H. Canty has been caught censoring a play produced by students entitled <i>Voices in Conflict</i>. The play is a compilation of firsthand accounts from soldiers who served in Iraq, including the words of 2005 Wilton High graduate Pvt. Nicolas Madaras, who was killed in September 2006 at the age of 19.<p>
The idea for the play came from drama teacher Bonnie Dickinson and was inspired by the book <i>In Conflict: Iraq Veterans Speak Out on Duty, Loss and the Fight to Stay Alive</i>. It also contained monologues taken from the documentary <i>The Ground Truth</i> and other sources.<p>
After complaints were made that the script for the play was "too antiwar," Dickinson added some monologues that took a less militant stance and edited out sections that were critical of George W. Bush, Donald Rumsfeld, or contained accurate depictions of death or "crude language."<p>
In all, seven characters were added to the original script and the ending was changed, but none of this was enough for Canty. He insisted that the play be canceled, and further prohibited the students from performing the play, even before a limited audience outside of official classroom hours.<p>
Perhaps Canty missed the underlying message of the school's recent production of Arthur Miller's <i>The Crucible</i>, which is widely considered to be a dramatic expression of the McCarthy witch-hunts. As a teacher in a drama program myself, it is deeply disturbing to me that students are being denied a very basic level of academic freedom. What's "crude" in this situation isn't the language--it's Mr. Canty's attempt to stifle debate about the war.<p>
As 17-year old cast member Sarah Anderson told the <i>New York Times</i>, "He told us the student body is unprepared to hear about the war from students, and we aren't prepared to answer questions from the audience, and it wasn't our place to tell them what soldiers are thinking."<p>
High school students who seek to bring the voices of soldiers fighting and dying in Iraq into their classrooms in order to shed light on the cruelty and brutality of this war should be commended, not reprimanded. I haven't gotten to read all of <i>Voices in Conflict </i>yet, but I plan to use the script as soon as I can in my own classroom.<p>
My question to Canty and the administration of Wilton High is this: Which is more of a threat to these students who are actively being recruited to be the next layer of cannon fodder for the so-called "war on terror"--the truth from the mouths of their former classmates, or complete and total ignorance?<br>
<b>Rebecca J. Lewis</b>, New Haven, Conn.<p>

