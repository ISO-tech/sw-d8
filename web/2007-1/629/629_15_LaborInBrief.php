<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">April 27, 2007 | Page 14</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Saigon Grill</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Dawning Greenstreet</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Delivery workers at the Saigon Grill in New York City have been picketing two of its three locations twice a day for the past month.<p>
The workers, calling themselves the Justice Will Be Served (JWBS) Coalition, are protesting against being locked out of their jobs after refusing to sign a false statement saying that they had been paid minimum wage and overtime pay. In truth, the workers had been paid just $1.60 an hour, far below the legal minimum wage of $4.85 an hour.<p>
Working conditions at the Saigon Grill, which makes about $2 million a month betweens its three locations, are deplorable. Workers are subjected to ridiculous fines, including $200 for customer complaints, and $50 for slamming a door or taking sick leave. They are also forced to pay for food that is lost if they are robbed while on a delivery, and are responsible for their own medical expenses.<p>
The workers were inspired by the recent victory of workers at 88 Palace, where workers successfully sued the restaurant for paying below minimum wage and stealing their tips.<p>
Yu Guan Ke has worked at the Saigon Grill for 10 years, making just $400 per month. Ke, who read about the victory at 88 Palace in a Chinese-language newspaper, decided it was time to take action. Picketers said in an interview that their actions have cut business in half<i>.<p>
