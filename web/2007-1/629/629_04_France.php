<font face="Times New Roman, Times, serif" size="5"><b>Winners and losers in France's vote</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 27, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>SHERRY WOLF analyzes the outcome of the first round of voting in France's presidential election.</b><p>

THE SECOND round of the French presidential election on May 6 will be a run-off between a narcissistic bully and an opportunistic pseudo-"socialist."<p>
In the first round last weekend, Nicolas Sarkozy, the candidate of the conservative Union for a Popular Movement (UMP), won 31 percent of the vote, while the Socialist Party's S&eacute;gol&egrave;ne Royal took 26 percent in a contest that brought an unprecedented 84 percent of the electorate to the polls.<p>
Fran&ccedil;ois Bayrou, from the center-right Union for French Democracy, who positioned himself as the gentleman-farmer candidate of the bosses came in well back with 18 percent of the vote.<p>
Curiously, in a country with nearly 10 percent unemployment that has been at the center of several successful social upheavals in the last two years, Sarkozy appears likely to win the second round, according to polls.<p>
But this appears to be more a matter of working-class dissatisfaction with Royal's moderate Socialist Party than enthusiasm for Sarkozy's pro-Bush and pro-ruling class politics. In recent years, the Socialist Party conceded to the bosses' privatization plans and a proposed anti-labor European constitution--positions soundly opposed by mass strikes and the overwhelming "non" vote on the constitution.<p>
In a campaign where anti-immigrant nationalism was adopted by Sarkozy to woo far-right Le Pen supporters, Royal played the nationalist card as well. She argued for the French to fly the flag on national holidays, hesitated to criticize right-wing eugenicist arguments from Sarkozy, and called for military boot camps for troubled youth.<p>
As a result, according to IPSOS polls, one-third of Socialist voters abandoned the party for other left-wing candidates or for Bayrou. And a disproportionately high number of those who ordinarily vote for left candidates were among the 15 percent who abstained from voting in the April 22 election.<p>
The <i>Washington Post</i> may have gotten it right when they wrote, "In the end, May 6 could be an unpopularity contest."<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">IN ADDITION to the record voter turnout, some of the most interesting results from the first round of the election were on the far right and left ends of the 12-candidate spectrum.<p>
The neo-fascist Jean-Marie Le Pen dropped considerably from the 17 percent of the vote he won in 2002, which put him into the second-round runoff with the conservative Jacques Chirac. This time, he came in fourth, with 10.5 percent.<p>
Evidently, Sarkozy's condemnation of Arab and African youth as "scum" and his call for establishing a new Ministry of Immigration and National Identity took anti-immigrant votes from Le Pen.<p>
On the left, the Communist Party candidate dropped to less than 2 percent of the vote, and the popular global justice activist and farmer Jose Bov&eacute; received only 1.3 percent. But the Trotskyist Olivier Besancenot of the Ligue Communiste Revolutionnaire (LCR) came in fifth place, with an impressive 4.1 percent--a total of 1.5 million votes.<p>
The LCR vote is of particular interest given widespread fears that votes for the far left might cause a repeat of 2002, when Le Pen narrowly beat the Socialist Party candidate to squeeze into the second round. Despite these concerns, a considerable number responded to Besancenot's uncompromising left-wing campaign for higher wages and equal rights for all African and Arab immigrants.<p>
As LCR member and author Daniel Bensaid said of the results, "The only valuable position is to remain independent...the ratio of force is rather favorable from the point of view of a unified force to the left of the left."<p>
It was unclear as <i>Socialist Worker</i> went to press how all the various parties will instruct their members to vote in the second round. However, the far-left parties, including for the first time Lutte Ouvri&egrave;re, are calling on their supporters to vote for the Socialist Party next month.<p>

