<font face="Times New Roman, Times, serif" size="5"><b>Behind the talk about Boston's "crime wave"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 27, 2007 | Page 10</font><p>

<font face="Times New Roman, Times, serif" size="3">A RECENT spate of homicides in Boston's Black and Latino neighborhoods has prompted city newspapers and politicians to begin talking about the need to crack down on street violence.<p>
In reality, the murder rate in Boston is at the average level for a city of its size. Nonetheless, many people are concerned about the murders and want to understand why all of this is happening.<p>
Throughout the course of the 1990s, the murder rate in Boston had been declining, but over the past several years, it has begun to climb again. Most of the murder victims have been poor and Black, but talk of the "crime wave" didn't reach prominence in the media or State House until a white tourist from New York City became one of the victims.<p>
In response, Massachusetts Gov. Deval Patrick and Boston Mayor Tom Menino have both pledged tens of millions of dollars to put more police on the streets.<p>
In addition, at the end of March, a vigilante group called the "Guardian Angels," led by right-wing talk radio host Curtis Sliwa, came up to Boston from New York City announcing its intention to patrol the streets against the "armed teenage marauders and cretins with chromosome damage who have paralyzed Boston."<p>
The racist and barbaric nature of this new "crackdown on crime" is already being felt. On March 27, an 11-year-old African American boy was arrested at his middle school and charged with unlawful possession of a firearm, unlawful possession of ammunition, unlawfully carrying a loaded firearm, and possession of a firearm on school property.<p>
The fifth-grade boy, who volunteers for the city Head Start program, had found the gun outside of the school, put it in his backpack, told his classmates what he had found, and then handed the gun over to his teacher. The boy spent the night in prison and is now awaiting trial.<p>
To many, it is clear that putting more police on the streets will not make the Black and Latino population of Boston any safer. Leonard Alkins, the head of the Boston NAACP, has called the Boston police's stepped-up presence in the Black neighborhoods of Roxbury and Dorchester nothing more than a "racist targeting of Black residents."<p>
Moreover, a 2006 study conducted by the University of Massachusetts-Boston, found that only 23 percent of Blacks in Boston felt "good or very good" in the city, and that only 21 percent had "confidence in the police."<p>
Over the past five years, members of the Boston police force have been investigated, indicted and convicted in local, state, and federal courts for wrongful arrests, wrongful killings, tampering with evidence, perjury and witness coercion. In one incident in 2004, police pepper-sprayed and then shot to death 58-year-old Luis Gonzalez, a mentally ill man who the cops had been called upon to prevent from committing suicide.<p>
Missing from the mainstream discussion of the Boston "crime wave" is an understanding of the ways in which the city itself produces "criminals" among the young, poor and Black.<p>
In a little-noticed article in the Boston <i>Phoenix</i>, it was pointed out that the murder rate in Boston dramatically increased in the years immediately following the 2001 recession, when then-Gov. Mitt Romney cut tens of millions of dollars from education and workforce development projects. Further budget cuts recently announced by current Gov. Deval Patrick can only be expected to exacerbate this problem.<p>
Indeed, the state of Black and Latino youth in Boston is dire. At least 30 percent of Black youth live in poverty, and according to Boston Public Schools report, 70 percent of public school students (85 percent of whom are non-white) qualify for free lunch. Unemployment remains much higher for Black youth than for whites.<p>
Further, the introduction in 2003 of rigid standardized testing in Boston public schools, known as MCAS, has led to higher dropout rates amongst low-income students, students of color and those with learning disabilities, according to the Massachusetts Department of Education.<p>
Given all of this, it is clear why many Black and Latino youth would lose hope and grow angry at the prospects of a dimly lit future. In the end, young people need jobs, well-funded education, health care and control over the ability to improve their lives, not demonization and police abuse.<br>
<b>Keith Rosenthal</b>, Boston<p>

