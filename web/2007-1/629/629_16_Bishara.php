<font face="Times New Roman, Times, serif" size="5"><b>Israel threatens Palestinian legislator</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 27, 2007 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">IN RESPONSE to the threat of secret charges against him, Azmi Bishara, the most prominent political leader of the Palestinian Arab citizens of Israel, has been forced to resign from office.<p>
Bishara was elected to the Israeli parliament, or Knesset, in 1996 and is a founder of the National Democratic Assembly (NDA), a party whose central plank calls on Israel to be a state of all its citizens.<p>
The exact nature of the charges against Bishara are unknown because Israel's Shin Bet security services placed a gag order on Israeli media outlets, which first reported rumors of impending charges in mid-April while Bishara was on a diplomatic trip to neighboring Egypt.<p>
Now Bishara has to decide whether to go into political exile, spend years fighting to prove his innocence of bogus charges, or face a long prison sentence. Given that he was the recipient of a kidney transplant from his brother, a long term in an Israeli prison would almost certainly kill him.<p>
For years now, the Israeli establishment has considered Bishara's eloquent advocacy of the rights of Palestinian citizens of Israel a threat. The powerful appeal of the NDA's demand that Israel become a state of all its citizens is intolerable to Israeli politicians.<p>
Israel claims to be the Middle East's only democracy, but its citizens are the Jews of the world--that is, not the 1 million Palestinians who remained--or descended from those who remained--after Israel's 1948 war to establish a state in Palestine. Some 800,000 Palestinians fled at the time after Jewish militias carried out massacres designed to provoke an exodus.<p>
The charges against Bishara are the latest chapter in Israel's decades-long drive to eradicate any and all expressions of Palestinian resistance to its colonial project of emptying Palestine of its indigenous Arab population. Bishara's NDA stands as a nagging reminder of Israel's hypocrisy and the leading representative of a force--a Palestinian civil rights movement--that has the potential to pressure Israel from within.<p>
This isn't the first time that Bishara has come under fire for his unapologetic defense of Palestinian rights.<p>
In 2001, the Knesset stripped him of his parliamentary immunity--the only time in its history it made such a move against one of its members--in order to charge him, among other things, with "incitement to violence" and "supporting a terrorist organization" (Hezbollah) because of a speech he gave that addressed the right, enshrined in international law, of an occupied people to resist occupation.<p>
Bishara deserves the support of all those who stand for justice and equality.<p>

