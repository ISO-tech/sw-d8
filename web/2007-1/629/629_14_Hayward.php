<font face="Times New Roman, Times, serif" size="3">Teachers at school sites have stood their ground against attempts to move scabs through picket lines. Less than 3 percent of regular teachers have crossed the lines. Picket lines also appeared outside the homes of school board members at 6:30 a.m.<p>
At Bowman Elementary, teachers established "strike schools" for younger children, who discussed Rosa Parks and wrote letters to the superintendent supporting their teachers.<p>
The district didn't count on such militancy and perseverance. On the seventh day of the strike, Vigil declared an educational state of emergency and publicly admitted that he had cobbled together just 180 of the promised 1,000 scabs to beat the union.<p>
"The district has been wasting time at the bargaining table for weeks by repackaging the same poor salary offer," said Kathleen Crummey, HEA president. "Now they want to go to court with more lies about our intentions and conduct. In the court of public opinion, this district has already been found guilty of gross disrespect of teachers, parents and students."<p>
Teachers throughout the state face similar contract fights. A union victory in Hayward will lead other school districts to think twice before attacking living standards and quality of education. Throughout the Bay Area, teacher unions have contributed tens of thousands of dollars to support a small but important strike fund for needy teachers.<p>

