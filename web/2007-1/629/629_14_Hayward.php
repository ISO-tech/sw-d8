<font face="Times New Roman, Times, serif" size="5"><b>Teachers hold the line in Hayward walkout</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 27, 2007 | Page 14</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>JOHN GREEN reports on a teachers' strike inspiring local support in a California town.</b><p>

HAYWARD, Calif.--The school district that sought an injunction against 1,300 striking teachers here pulled back after the teachers' union rebutted the district's filing at the California Public Employment Relations Board (PERB).<p>
The Hayward Unified School District filed for an injunction with the PERB, the first step in seeking a court order to end the strike that began April 5. The district retreated, but threatened to press for the injunction if the union didn't back down from its demands.<p>
The 1,300 teachers, represented by the Hayward Educators' Association (HEA), have launched a highly visible, militant strike. Given their high health care costs, Hayward teachers are among the lowest compensated in the expensive Bay Area. They're asking for the same 16.84 percent raise granted top district administrators this year.<p>
Spirited "unity rallies" have brought together hundreds of teachers, parents and students in daily shows of force throughout Hayward. A parent solidarity group, Families in Action, led 1,500 parents and students in a march on the district office to gain a hearing with Superintendent Dale Vigil.<p>

