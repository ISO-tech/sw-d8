<font face="Times New Roman, Times, serif" size="5"><b>The IRA's bid for respectability</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 16, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>BRIAN KELLY reports from Northern Ireland on the latest concession to respectability by the Provisional Irish Republican Army (IRA).</b><p>

THE PROVISIONAL IRA's final step in its long march to respectability came on January 28, when Sinn Fein delegates at a special convention voted overwhelmingly to endorse the Police Service of Northern Ireland (PSNI).<p>
Though the Sinn Fein leadership seem to have sold much of the party grassroots on the notion that joining the policing boards will help them to "put manners on the police," in reality, the move represents a stunning capitulation to intense pressure from the British and Irish governments, the Bush administration and their new friends in Corporate Irish-America.<p>
The vote will come as a huge relief to the British government, which has stalled every attempt to lift the lid on its dirty war in Ireland. Some 74 percent of the "reformed" PSNI are former members of the Royal Ulster Constabulary (RUC), the sectarian police force that beat civil rights marchers off the streets, led pogroms on nationalist ghettoes in Derry and Belfast in 1969, and colluded for nearly 30 years in a sectarian murder campaign carried out by Loyalist paramilitaries.<p>
Just a week before the Sinn Fein convention, the Blair government had been deeply embarrassed by Police Ombudsman Nuala O'Loan's report into collusion between the police Special Branch and Loyalist death squads in a small corner of north Belfast.<p>
O'Loan attributed the murder of a young Protestant to Loyalist paramilitary and paid police informer Mark Haddock--and revealed that Haddock was implicated in at least another 15 murders while on the police payroll in the early 1990s, many of them sectarian murders of innocent Catholics.<p>
The report had the potential to lift the lid on systematic collusion involving literally hundreds of such murders stretching back to the 1970s, but with Sinn Fein on board, it is unlikely that the Blair government will feel compelled to yield to public scrutiny. The British government's decision to push ahead with building a new multimillion-dollar headquarters for its spy agency MI5 on the outskirts of Belfast shows just how worried it is of being "hollowed out" by Sinn Fein's presence on the policing boards.<p>
Sinn Fein's vote confirms the remarkable transformation of the Provos from a guerrilla army based in the working-class nationalist ghettoes into a slick political machine, anxious to prove its credentials to the corporate world.<p>
During the brief time when the Northern Ireland Assembly was up and running, Sinn Fein ministers helped usher in privatization of the National Health Service, and in recent months, party spokespeople echoed private-sector calls to lower corporate taxes and cut public-sector employment in the north.<p>
In the face of New Labour's plans to compel residents to finance privatization of the water service, Sinn Fein has come out openly against a non-payment campaign--despite widespread support for such a campaign in Protestant and Catholic working-class communities across the north and the active support of the trade union movement.<p>
Inevitably, Sinn Fein's attempt to shed its radical image will lead it into confrontation with the hard-pressed communities that sustained resistance against the British occupation--and which today find themselves threatened by the party's new friends in Downing Street, Washington and corporate boardrooms the world over.<p>

