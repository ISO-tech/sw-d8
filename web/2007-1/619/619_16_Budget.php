<font face="Times New Roman, Times, serif" size="4">$1.7 billion a day for the Pentagon</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bush's budget for permanent war</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 16, 2007 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">THE GUNS, guns and more guns budget. That's the Bush administration's proposed federal budget for 2008.<p>
There's $481.4 billion for Pentagon spending for the fiscal year beginning October 2007--not including supplemental appropriations requests of $141.7 billion to pay for the wars in Iraq and Afghanistan. That adds up to $623 billion for the war machine.<p>
Tucked away in funding for the current wars in Iraq and Afghanistan is a Pentagon weapons wish list, including the F-22 fighter ($4.6 billion), the SSN-774 Virginia attack submarine ($2.7 billion), Trident D-5 submarine-launched ballistic missiles ($1.2 billion) and ballistic missile defense ($10.8 billion).<p>
"What's remarkable about this year's military budget is that it's the largest budget since World War II, but, of course, we're not fighting World War II," the World Policy Institute's William Hartung told the Inter Press Service. "We're fighting terrorist networks armed with explosives and AK-47s. This has to be considered a triumph of an arms lobby that can obviously sell us things we don't need at a time that the president claims we're in mortal danger."<p>
In fact, the "war on terror" is proving to be a huge boon to weapons makers and war profiteers. The proposed military budget exceeds that of the Vietnam War and is almost double the government's Cold War budget in today's dollars, amounting to $1.7 billion a day.<p>
The Pentagon isn't the only beneficiary of George W. Bush's budget, however. The Department of Homeland Security is pegged for an increase of $2.3 billion over last year's spending--a total of $34.4 billion.<p>
A whopping $1.8 billion will go to a "virtual fence" on the border, 3,000 new Border Patrol agents and 600 new detention beds. The FBI's budget will increase 13 percent, to $6.4 billion.<p>
The money that Washington showers on the military is coming from the budget for schools and services for workers and the poor.<p>
According to the Office of Management and Budget, military spending is greater than spending on veterans' benefits, education, environmental protection, housing assistance, transportation, job training, agriculture, energy and economic development combined.<p>
Funds allocated for education, training, employment and social services come to just 3 percent of the budget, or $82.7 billion--<i>down </i>by 12 percent. Medicare and Medicaid are set to lose more than $100 billion--a devastating blow.<p>
Meanwhile, veterans' benefits will increase from $72.4 billion to $83.4 billion, but that's short of what's needed to keep up with the growing demand for services. According to the Veterans Administration, the number of patients using its health care system has risen from 3.8 million in 2000 to 5.5 million in 2006. Also included in the Bush administration plan is an increase in fees and a doubling of co-pays for low-income veterans' pharmaceuticals.<p>
This might be the Bush administration's budget, but the Democrats won't do much to stop it.<p>
Some politicians in Washington--Democrats and Republicans--are speaking out about deep cuts in social services, such as California Gov. Arnold Schwarzenegger, who fears the local outcome when federal cuts go into effect.<p>
But Democratic Party leaders are agreed with Bush that military spending can't be cut while troops are in Iraq. Instead of demanding an immediate withdrawal of troops in Iraq, many Democrats are preaching "fiscal responsibility." For instance, Sen. Joseph Lieberman (D-Ct.) is proposing a "war on terrorism tax," to meet the rising cost of national defense.<p>
Bush wants to spend $1.7 billion a day on war. That amount of money could go a long way toward repairing the damage the U.S. done in Iraq--where millions go without electricity and clean water as a result of war, occupation and more than a decade of sanctions before that.<p>
Antiwar activists are pressuring Democrats who say they want an end to the war to back up their words with action--by withholding funding for the war. Activists from the Occupation Project are sitting in on congressional offices to demand that Congress reject the 2007 supplementary spending bill--scheduled for a vote in early March.<p>
If passed, the bill would authorize $100 billion more to the Iraq occupation. Protest and organization can make the politicians understand that we won't stand by while they fund more killing and starvation in Iraq.<p>

