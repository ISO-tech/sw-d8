<p><a name="Islam"></a><font face="Times New Roman, Times, serif" size="4"><b>Criticism of Islam</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I AGREE with fighting against racism, be it aimed at Muslims, Jews, Christians or those of no faith. But where do we draw the line between free criticism of religious beliefs and racism?<p>
Does <i>Socialist Worker</i> believe all religion to be sacrosanct and beyond reproach? If someone of any religion, be it Islam, Christianity or another, says something that you disagree with, should you be entitled to peacefully and without insult express your disagreement? Or should you stay silent and regard even uttering such disagreement as a racist slant on their faith?<p>
Demonizing entire communities or an entire faith is wrong. But no religion is perfect, and there are extremists in all religions. In my view, a Muslim who preaches hatred toward homosexuals is not better or worse than a Christian who preaches the same.<p>
Socialists should continue to fight racism and bigotry, but they should never demand that the law protects people's beliefs and views from respectful non-racist criticism and disagreement.<br>
<b>Dan Factor</b>, London<p>

