<a name="Undermining"></a><font face="Times New Roman, Times, serif" size="4"><b>Undermining our democracy</b></font><p>

<font face="Times New Roman, Times, serif" size="3">REGARDING A February 1, 2007, Associated Press story concerning Venezuela, I wish the lack of substantial information surprised me. However, the grim reality is our corporate media is no more than a propaganda machine for the White House.<p>
Most disturbing in the story are the last two paragraphs--specifically, George Bush's quote, "I am concerned about the undermining of the democratic institutions and we're working to help prevent that from happening."<p>
If only Bush had the same concern over the undermining of democracy in the United States. Let us take a moment to consider the following events: signing an act that undermines the basic civil liberties of every citizen in our nation (the USA PATRIOT Act); spying on private citizens and opening first-class mail; altering nearly every bill signed into law to suit alternative agendas using signing statements.<p>
Similarly, using manufactured intelligence to wage war on Iraq and censoring government scientists and their findings about global warming severely damages our global reputation. Furthermore, let us not forget the questionable election results of 2000 in Florida and the recent indictments of election workers in Ohio concerning the 2004 election.<p>
Most recently, take into account the directive giving the executive branch greater control over rules and policy statements that the government develops to protect public health safety, the environment, civil rights and privacy.<p>
Whose democracy is really in peril? The fact that the U.S. conducts business with dictatorships like Saudi Arabia and China with human rights records that would make leaders of the former Soviet Union candidates for sainthood further begs the question as to why the White House is so preoccupied with this little South American country. Could it have any thing to do with the corporate takeover of the White House?<p>
While the U.S. government is framing Venezuela as the next country that requires rescue from "anti-democratic forces," somebody should remind the president that the U.S. Constitution begins with "We, the people," not "We, the corporations."<br>
<b>Chris Armitage</b>, Endicott, N.Y.<p>
