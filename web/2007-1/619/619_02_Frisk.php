<font face="Times New Roman, Times, serif" size="4">Report on stop-and-frisks reveals...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Racial profiling by the NYPD</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jared Rodriguez</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 16, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">A NEW report released last week by the New York Police Department on its stop-and-frisk statistics for 2006 has added fuel to the blaze of outrage ignited by the police killing of Sean Bell on his wedding day last November.<p>
More than half a million people were stopped by the NYPD in 2006, according to the report, a 500 percent increase in police stops since 2003. Arrests doubled over the same period. The report also shows that almost 86 percent of all those stopped were Black or Latino, showing that the NYPD's practice of racial profiling is alive and well.<p>
The quarterly reports issued by the NYPD detailing information about police stops are the result of anti-police brutality protests that swept across New York City in the wake of the NYPD killing of Amadou Diallo, who died in a hail of 41 police bullets in 1999.<p>
According to the ACLU, the NYPD has not issued a stop-and-frisk report since 2003, even though they are mandatory. "The department's failure to comply with this basic rule for over three years raises serious questions about its commitment to eliminate racial profiling and other forms of race-based policing," said Donna Lieberman, executive director of the New York Civil Liberties union.<p>
Rev. Al Sharpton held a press conference on Super Bowl Sunday threatening to file a class-action network on behalf of all those illegally stopped.<p>
The report is further proof of what activists have been saying for years--that while the NYPD had racially diversified its force, the practice of racial profiling has grown sharply, with September 11 attacks in particular providing the justification.<p>
Meanwhile, tensions remain high in New York City as Queens District Attorney Richard Brown drags his feet on the decision about indicting the cops who murdered Sean Bell. Bell's killing in the early morning hours of November 25--when police opened fire on a vehicle he was sitting in with two friends--sparked a huge protest numbering as high as 40,000 in December.<p>
The latest report on police stops only reinforces the urgency of organizing a fightback.<p>

