<font face="Times New Roman, Times, serif" size="4">Teachers say: "I don't want to go on strike, but I will"</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Rallying for teachers at SFSU</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Kristin Anderson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 16, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Approximately 300 people attended a rally held by the teachers' union at San Francisco State University (SFSU) February 9 to protest cuts in the California State University (CSU) system and to demand a fair contract for faculty.<p>
Myra Fowler, the current president of the Associated Students, pointed out at the rally that, during the 2002-2003 and 2003-2004 school years, state funding for the CSU system was slashed by $511 million.<p>
Undergraduates saw a fee increase of 76 percent, and graduate students saw an increase of 106 percent. Meanwhile, classes and entire programs were cut--and students were forced to vote to raise their own fees so that they would not lose the athletics department.<p>
At the same time, the top executives in the CSU system received a pay increase of 23 percent. SFSU president Robert Corrigan is paid $261,144, gets free housing and receives a $12,000 car allowance.<p>
Linda Ellis, the president of the California Faculty Association at SFSU and chair of the museum studies department, said at the rally that the cuts in funding are leading to a decline in the quality of education. Teachers are being given larger classes and advisors are responsible for more students.<p>
Teachers are currently threatening to hold a two-day strike. "By conceding to the CSU's offer, we not only resign ourselves to an unfair deal, we set ourselves up for future setbacks, such as more takeaways on faculty rights and deeper inequalities in the pay structure," the union said in a statement. The teachers rallied behind the slogan, "I don't want to strike, but I will."<p>
They were supported at the rally by Democratic State Sen. Leland Yee, members of AFSCME Local 3299, the CSU Employees Union and the SFSU Associated Students. Associated Students has passed a resolution supporting any strike action that the teachers decide to take, and the teachers' union has asked students to sign strike pledges promising not to cross the picket line if the teachers do decide to strike.<p>
Teachers led the crowd of 300 in a short march past the administration building chanting, "Fewer classes, higher fees--the CSU is run by thieves!"<p>

