<font face="Times New Roman, Times, serif" size="4">Florida rape victim arrested and denied treatment</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"A victim of the system"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 16, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">COMPASSION FOR victims is apparently at the bottom of the list of priorities for Florida police. That's the only conclusion that can be drawn after Tampa police jailed a 21-year-old rape victim for two days in late January--only releasing her after a massive public outcry.<p>
The young woman was raped during Gasparilla, an annual parade that draws thousands of people to the area. After calling police to report the crime, she was taken to a rape crisis center, where she received medical care and a dose of emergency contraception.<p>
Later, as she was riding with police in a patrol car, trying to locate the scene of her rape in the dark, police were alerted that she had a warrant from another county stemming from allegedly outstanding fines from a 2003 juvenile arrest. The woman says she believed she had paid the fines, and was unaware of the warrant.<p>
According her lawyer, Vic Moore, once police learned about the warrant, they "stopped the investigation right there," put her in handcuffs and then behind bars in the county jail, where she stayed for nearly two days.<p>
And her outrageous treatment by authorities only got worse. Despite the fact that a second dose of emergency contraception needs to be taken within 12 hours of the first dose to be most effective, a nurse at the jail would not let the woman take her second dose--reportedly citing religious objections for withholding the drug. The young woman did not receive the second dose of the medication until just before she left the jail--nearly two days later.<p>
"I was in total disbelief," the woman's mother told reporters. "You just can't imagine the fury that was going through me. How dare that person force their religious beliefs upon my daughter in such a way that it may harm her?"<p>
An investigation has now been opened into the refusal of the medication, and Tampa police have offered an apology to the woman for putting her in jail, saying they have amended their policy to use "discretion" in such cases involving rape and sexual assault victims.<p>
But this is not the first time Florida police have acted so callously. In a similar situation in 2001, St. Petersburg police threw a rape victim in jail for three days on outstanding warrants. As Vic Moore told reporters about his client, "She is not a victim of any one person. She is a victim of the system."<br>
<b>Nicole Colson</b>, Chicago<p>

