<font face="Times New Roman, Times, serif" size="5"><b>Behind ICE's raid in New York City</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Petrino DiLeo</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 16, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">USING THE pretext of busting up a money-laundering operation, U.S. Immigration and Customs Enforcement (ICE) conducted its boldest raid ever in New York.<p>
About 200 ICE agents and New York police officers raided 24 wire-transfer stores in Queens--mostly clustered in the heavily immigrant neighborhood of Jackson Heights--rounding up 27 people in the process. Of those, about 10 are undocumented and are being held indefinitely at the Federal Detention Center in New York City, awaiting trials.<p>
The police department said the raids were the result of a two-year investigation into an alleged money-laundering scheme in which $2 million in drug money was funneled to Colombia.<p>
To produce the arrests, the undercover police agents approached remittance agencies to conduct illegal transfers for two years--ironically using money seized in other drug raids. These tactics produced a list of suspects who were later arrested.<p>
ICE has been operating in New York for years, rounding up individuals and families in small operations. But the raid in Jackson Heights--named Operation Pinpoint--is believed to be the first time ICE agents have been used as part of a criminal investigation in New York City. The tactic is similar to the raids at six Swift meatpacking plant in December, where the government used the story of breaking up an identity-theft ring as excuse for the round-up.<p>
The remittance agencies--sometimes called people's banks--are the only option for many undocumented workers to send funds back to families in other countries. Though the agencies are rightly criticized for taking advantage of undocumented workers and charging exorbitant fees and interest, they do provide a vital service.<p>
According to the World Bank, remittances are the largest source of external finance for developing countries, exceeding the amount they receive in foreign direct investment or foreign aid every year. In 2006, the World Bank expects remittance flows to developing countries will surpass $200 billion.<p>
As a result of the raids, about half of the 24 wire-transfer businesses are completely shut down, and others have been open fewer hours than usual.<p>

