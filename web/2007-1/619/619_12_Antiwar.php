<font face="Times New Roman, Times, serif" size="5"><b>Marches aren't about celebrities</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 16, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">CHARLES JENKS of the Traprock Peace Center writes on the January 27 protest in Washington, D.C. <a href="http://www.traprockpeace.org/traprock_blog/index.php/2007/02/03/marchers-send-message-and-deserve-better/" target="_blank">Click here</a> to read the full article.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">ON JANUARY 27, the people sent a clear message to Washington: "Get U.S. troops out of Iraq now!" Hundreds of thousands of people marched, and they completely--for the first time in history, it is reported--surrounded the Capitol building. When the first marchers came to the end of the loop, there were people still waiting to start marching.<p>
Unfortunately, United for Peace and Justice (UFPJ), the primary sponsor, didn't live up to the standards set by the marchers. Its continuing refusal to work with some other national coalitions, and its focus on celebrities and politicians, was reflected in its botching the start of the march and focusing on media coverage.<p>
The great news, though, from my perspective, is that this march drew such a broad range of people. Look at the people marching--<a href="http://www.traprockpeace.org" target="_blank">www.traprockpeace.org</a> has over 200 march photos--and you'll see a cross-section of America. This, as much as the numbers, is what should worry the Bush administration and Congress.<p>
I saw the entire march, as I was assigned to photograph it for Traprock Peace Center (which has covered every national march since the historic gathering in Washington on October 26, 2002). This was as large as any march that I have seen in D.C. The 500,000 estimate given by organizers seems reasonable.<p>
This was a huge outpouring of people, despite it being January, and despite reports that there would be a wind chill in the 30s, with 15 mile-per-hour winds. As it turned out, it was almost balmy, with little wind and temperatures in the 40s. If the weather forecast had been accurate, surely the march would have been even larger (No, I'm not blaming Bush for messing with the weather report, though it did occur to me!).<p>
This writer saw not one instance of violence. An eyewitness told me that at one point about 80 people (150 people, according to the Associated Press--funny what the media exaggerates, and what it downplays) rushed down a sidewalk at the Capitol as though they were going to storm up the Capitol steps.<p>
This sent the police scurrying to head them off, with police running down the steps. The rush was obviously choreographed and done in jest, as protesters came to a sudden halt, apparently acting merely to tease the police and get a reaction. This kind of behavior, juvenile in my opinion, was the marked exception on this day.<p>
The mainstream media, of course, grossly underreported the size of the march. The Associated Press called it "tens of thousands," and cited police sources as saying it was less than 100,000. So what else is new? Organizers obviously need to take media tendencies into account ahead of time. Was that done here? It didn't seem so.<p>
This march was about the people who came to protest the war and occupation. It wasn't about the celebrities and politicians who gave it a glamorous face and allowed march organizers to rub elbows with them.<p>
Please understand me--I am glad that celebrities and pols participate. Yet media coverage would lead one to believe that it was all about the celebrities leading "tens of thousands." The huge masses of people were the real story, but these people weren't in the story.<p>
UFPJ needs to get off its high horse about being <i>the</i> coalition of antiwar forces in the U.S., as it represented itself before the London International Peace Conference in December 2005 (In the next breath, UFPJ told international organizers that it was not going to participate in global mass demos in March 2006, preferring instead a mass demo in April as a way to have influence on the November elections).<p>
If UFPJ wants to end this war and occupation now, it needs to become a willing and cooperative partner with other national groups, including ANSWER, and get over its fixation on celebrities and liberal politicians.<p>

