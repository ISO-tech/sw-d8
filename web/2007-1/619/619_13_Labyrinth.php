<font face="Times New Roman, Times, serif" size="4"><i>Pan's Labyrinth's </i>antifascist message</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Reaching for a dark rose</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Jim Ramey</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 16, 2007 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i><a href="http://www.panslabyrinth.com" target="_blank">Pan's Labyrinth</a>,</b></i> written and directed by Guillermo del Toro, starring Ivana Baquero, Sergi L&oacute;pez and Ariadna Gil.</font><p>

<font face="Times New Roman, Times, serif" size="3">A CENTRAL theme in <i>Pan's Labyrinth</i>,<i> </i>the Oscar-nominated film set in fascist Spain, is the story of a dark rose that blossoms every night at the top of a hill. The rose promises eternal life to anyone who can pluck it but is surrounded by its own poisonous thorns.<p>
After years of seeking without success the rose's eternal life, talk of the rose is only about the horrible deaths that its thorns cause and not about the promise that its bloom holds.<p>
Every character in this film, except one, has deeply considered the rose's promise, and each has derived their own answer that leads neither to immediate death nor eternal life. The main character, Ofelia, is the exception.<p>
Set in 1944 fascist Spain, the Spanish Civil War of the 1930s is discussed in hushed and paranoid tones. Every character exists under the heel of the movie's personification of fascism, Captain Vidal.<p>
He has been stationed at a fort converted from an old mill at the base of a mountain that is home to an active band of guerrilla fighters. He is there to destroy all resistance in the area.<p>
Early on, the audience is given a searing, memorable display of the brutality Vidal is capable of when he interrogates a father and son who are hunting rabbits for food. The camera holds steady on the son as the captain repeatedly plunges a broken bottle into his face.<p>
It's important to note that an active resistance to fascist dictator Gen. Francisco Franco did exist throughout most of his 36-year rule. The Spanish maquis organized thousands to fight fascism in Spain and southwestern France where they are given credit for helping to liberate 17 towns from Nazi rule.<p>
But in the time period of <i>Pan's Labyrinth,</i> they were already being arrested by the dozens and became less and less effective as the decades wore on.<p>
Ofelia is introduced to Vidal through her mother Carmen. What we know about Carmen is that her husband, Ofelia's father, was a tailor who was killed in the civil war and that she is pregnant by Vidal.<p>
Wanting to smooth out the relationship between Vidal and Ofelia, a relationship she knows will be rocky, Carmen implores her daughter to call him father, saying, "It's just a word Ofelia, just a word."<p>
The impression given is that Carmen has seen the dark rose, and she's seen the corpses of her loved ones in its thorns. She can't bear the thought of seeing her daughter there too.<p>
Ofelia is of a different mind. Carrying the books of the Brothers Grimm around as a revolutionary would carry Marx and Engels, she has her mind set on adventure.<p>
At an old labyrinth near the mill, she meets a faun who tells her that she is the lost princess of an underground realm, but to be sure she must complete three tasks to prove it to him. The three tasks cause Ofelia to confront the fear and uncertainty that she has as a girl old enough to understand the world around her but not old enough to intervene and change things for the better.<p>
The fantasy world that she creates is less escapism than it is training for how to confront Captain Vidal. The film is brilliant in this regard because it never makes it clear who Ofelia should trust.<p>
Fairies don't look like Tinkerbell in <i>Pan's Labyrinth,</i> and the faun who gives her each task is at once lovely and monstrous with an agenda as clear as coal. She learns that following others' advice blindly--no matter how authoritative--will lead to destruction, but also that alone and without a path to follow she can't even get out of a locked room.<p>
The character that Ofelia trusts most of all is Mercedes, a spy for the guerrillas who works for Vidal. Like Ofelia, Mercedes must play a part that she loathes to accomplish a goal to which she is committed.<p>
The bond between the two characters is immediate and strong, and it's through Mercedes that hope in the real world is carried through to the end of the film.<p>
The Spanish Civil War is always on the corners of this film. <i>Pan's Labyrinth</i> begins with death and ends with hope that turns Spanish history in this period on its head but it never falls into the trap of cynicism.<p>
A revolution defeated, especially one of the magnitude of Spain in the 1930s, is tragic, but the defeat doesn't mean that the fight shouldn't have happened. What lives on are the lessons we can take from defeat and the promise of a dark rose that blooms every night.<p>

