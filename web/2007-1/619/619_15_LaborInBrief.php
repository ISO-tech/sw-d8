<a name="Mass"></a><font face="Times New Roman, Times, serif" size="4"><b>UMass graduate employees</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By James Fiorentino</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AMHERST, Mass.--More than 100 University of Massachusetts (UMass) students and workers, joined by worker delegations from two other unions, attended a rally held in support of the Graduate Employee Organization (GEO-UAW 2322).<p>
The union is focusing its campaign for a new contract around four major demands.<p>
The first is a total rollback of student fees, which have been steadily climbing virtually every semester. The fees are largely used to help the university service its massive debt and pay for construction projects that employ non-union labor on campus.<p>
The second demand is for the university to take immediate and concrete steps to implement a comprehensive affirmative action hiring and recruitment plan. UMass has a poor record of attracting and retaining students and faculty of color--and $800,000 earmarked by the university to combat this problem has "disappeared."<p>
The third and fourth demands involve increased access to affordable, fair, and decent housing for graduate students and their families. Graduate housing at UMass is of low quality, and hasn't been improved in 10 years.<p>
Despite low pay and poor living conditions, the labor that graduate students perform is central to the day-to-day operation of the university.<p>
At the rally, electricians from an IBEW union local called for solidarity with the UMass grad students and all other unions on campus during the negotiation period. AFSCME, which represents a large number of university staff, is also negotiating a new contract.<p>
A shop steward from Iron Workers Local 7 spoke of the need to force the administration to hire unionized companies for any job that requires subcontracting. UMass has embarked upon an expensive construction effort, and about half of the workers hired by the university are non-union.<p>
Eddie Dejoinville, a 35-year-old member of Local 7, said that he came out to support the UMass employees because "unions are important." "I worked for 10 years at a non-union company, and my quality of life was really suffering," he said. "The union gives me excellent benefits and job security. Joining the union has been a big improvement."<p>
Ironworker Emmett Holland echoed Dejoinville's remarks. "Every worker should be in a union," he said.<p>

