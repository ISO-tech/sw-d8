<font face="Times New Roman, Times, serif" size="5"><b>Tensions rise over British boats in Iranian waters</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 30, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">TENSIONS CONTINUE to build between Iran and the U.S. and British governments after Iran captured 15 British sailors aboard two vessels that had illegally entered Iranian waters on March 23.<p>
British officials demanded the sailors' immediate release. But Iran announced that it was interrogating the sailors to find out whether they had purposefully entered Iranian waters--and determine whether they should be tried for espionage.<p>
A day later, the United Nations (UN) Security Council announced a new round of sanctions against Iran for its refusal to abandon an nuclear enrichment program. In response, Iran partially suspended cooperation with the International Atomic Energy Agency, the UN nuclear watchdog agency.<p>
The U.S. and its allies insist Iran is seeking to build nuclear weapons, even though Iran--a signatory to the Nuclear Non-Proliferation Treaty--is exercising its right under that treaty to develop a nuclear energy program.<p>
News of the new sanctions against Iran and the diplomatic standoff over the detained British sailors drove oil prices to $63 a barrel, the highest price this year.<p>
British Prime Minister Tony Blair demanded the release of the captured soldiers as a "fundamental" issue for his government, and insisted the British vessels were in Iraqi waters on a UN-approved mission. But he refused to release details of the ships' whereabouts when they were stopped by Iran.<p>
This wouldn't be the first time that British ships violated Iran's waters. Iran captured eight British service members in 2004 under similar circumstances, releasing them in good condition after three nights in captivity.<p>
Though the British are insisting on the rapid return of their sailors, five Iranians detained in Iraq earlier this year are still being held by the U.S.<p>

