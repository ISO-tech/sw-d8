<font face="Times New Roman, Times, serif" size="5"><b>Socialism for the 21st century</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 30, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">ORGANIZING IS underway for <a href="http://www.socialismconference.org" target="_blank">Socialism 2007</a>, a conference set for June 14-17 in Chicago. The theme of the event--which for the past several years has drawn more than 1,000 people from across the U.S. and the world--is "Socialism for the 21st Century."<p>
Socialism is sponsored by the Center for Economic Research and Social Change (publisher of <i>International Socialist Review</i> and Haymarket Books), and co-sponsored by the International Socialist Organization, publisher of <i>Socialist Worker</i>.<p>
This year's Socialism is shaping up to be one of the most exciting conferences yet, with featured speakers including prominent voices of the left, including Amy Goodman of <i>Democracy Now!</i> <i>CounterPunch's</i> Jeffrey St. Clair, <i>Air America</i> host Laura Flanders and <i>Nation </i>magazine columnist Dave Zirin.<p>
Activists on the front lines of the antiwar movement will attend, including Kelly Dougherty, Camilo Mej&iacute;a and Garrett Reppenhagen of Iraq Veterans Against the War, and Charles Jenks of the Traprock Peace Center.<p>
Those fighting the criminal injustice system will also be on hand: Yusef Salaam, exonerated in the Central Park jogger case; Barbara Becnel, co-author of several books with executed California prisoner Stan Tookie Williams; and members of the Campaign to End the Death Penalty and Free Gary Tyler Committee.<p>
Online registration will be available soon, with a discounted price for those who register before May 15--visit <a href="http://www.socialismconference.org" target="_blank">www.socialismconference.org</a> for more information.<p>

