<font face="Times New Roman, Times, serif" size="5"><b>NEIU witch-hunt collapses</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Rachel Cohen</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 30, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--The bravado of the ringleaders in a witch-hunt against socialists at Northeastern Illinois University (NEIU) went out with a whimper March 7.<p>
Taking a cue from university administrators, who were maneuvering to discredit the campus Socialist Club following a protest against the presence of CIA recruiters, the Republican-led Student Government Association (SGA) had moved to decertify the club.<p>
Two Socialist Club members were arrested February 28 for simply trying to attend the meeting with CIA officials.<p>
After a series of e-mails exchanging contrived condemnations and plots, the SGA's rules and charter committee summoned the club's president, Keeanga Taylor, to appear for a "hearing" on the Socialist Club's official charter.<p>
Amid the messages traded among SGA senators--leaked to Taylor and forwarded out to a variety of campus activists and organizations--was hate mail from one campus employee volunteering her approval of the Student Government's decision to "hang" the socialists, as they were "guilty by association."<p>
Rather than be intimidated, the club took their hearing as an opportunity set the record straight, and to put campus repression on trial instead.<p>
Socialist Club members, it turned out, were not alone in wanting to know why the SGA had time to put the club on trial when they had done nothing to stand up to the 14.7 percent tuition hikes the student body will face next fall.<p>
Nor has the SGA lifted a finger to question the decertification of about half the school's student organizations last semester on the basis of Student Activities Department's bureaucratic and arbitrarily enforced codes and regulations.<p>
In fact, the Socialist Club was joined by 40 other supporters, activists and other students, who packed the meeting and applauded Taylor's speech condemning the political persecution of the socialists and demanding action, instead, on behalf of NEIU's working-class student body.<p>
The SGA was forced to relent. It even voted to demand that the administration make public its investigation of the incident and to interview the students involved--which NEIU officials still have yet to do.<p>
This victory was made possible by the unpopularity of the SGA's well-known prowar bias and their neglect of students' concerns. But it was also a result of the polarizing effect the anti-left campaign by the administration and the SGA.<p>
On the one side is a growing Socialist Club, an ever-more-determined Campus Antiwar Network and a broad layer of students, faculty and staff appalled by campus repression. On other side is an isolated administration that can't get its story straight.<p>
On the day of the first court appearance for the two men arrested at the CIA event, the university lawyer reduced the charge against student Kenneth Barrios from petty charge of "disrupting a university event" to a misdemeanor battery charge. Another student, Matthew Larson, also faces a simple battery charge.<p>
Activists at NEIU will need the help of <i>SW</i> readers nationwide to keep up the stream of phone calls, e-mails and petition signatures going to NEIU's administration to get these phony charges dropped.<p>
