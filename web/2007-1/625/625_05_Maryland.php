<font face="Times New Roman, Times, serif" size="5"><b>Death penalty repeal halted in Maryland</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 30, 2007 | Page 5</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>ALEX BENNETT reports on the fate of legislation to abolish the death penalty in Maryland.</b><p>

MARYLAND ISN'T shutting the door on its death row--yet. The state Senate's Judicial Proceedings Committee blocked legislation to repeal the death penalty by a single vote earlier this month.<p>
Maryland had a chance to become the first state to abolish the death penalty since the reinstatement of capital punishment in 1976. With mounting doubts about lethal injection halting executions from California to Florida, legislators are considering repeal bills more seriously than ever before.<p>
Momentum toward abolition in Maryland began to pick up in December, when the state Court of Appeals, considering the case of Vernon Evans, ruled that Maryland's lethal injection protocols were introduced without the public review required by law. The decision produced a de facto moratorium on executions.<p>
Activists launched a comprehensive campaign to push for repeal: organizing demonstrations, letter-writing campaigns and panel discussions that included death row exonerees, murder victims' family members, along with call-ins from death-row prisoners.<p>
When the newly elected Gov. Martin O'Malley came out strongly for abolition, victory finally seemed to be within reach. But the repeal bill was never even debated on the Senate floor--the Judicial Proceedings Committee's vote stopped it.<p>
According to the <i>Baltimore Sun</i>, state Sen. Alex Mooney, the most promising of the potential swing votes on the committee, declared that "a full and absolute repeal of the death penalty under all circumstances is not in the best interest for the common good of Maryland's citizens."<p>
One might wonder which of Maryland's citizens Mooney was referring to. Certainly not the African-American men on death row, who a state-commissioned study found were many times more likely to get a death sentence because of their race and the jurisdiction they were tried in. Certainly not people like death row exoneree Kirk Bloodsworth, who would have been executed if not for the discovery of DNA evidence proving his innocence. Certainly not murder victims or their families, since states (like Maryland) that practice capital punishment actually have higher murder rates than those that don't.<p>
Mooney can only be speaking for the class of citizens who benefit from a "justice" system that places a higher priority on locking people up than rehabilitating them. By pinning the blame for society's problems onto the exploited and oppressed rather than the exploiters and oppressors, the criminal justice system tries to keep working-class communities paralyzed with fear--and validate public policy that sacrifices social services like education and welfare in favor of building more prisons and saturating city streets with cops.<p>
The death penalty is the ultimate emblem of this ideology, scapegoating individuals who we supposedly have no choice but to kill in cold blood if we wish to be safe and free. The racism, class bias and ineffectiveness of the death penalty are endemic to the system as a whole.<p>
This is why judges and lawmakers are currently focusing on procedural problems with lethal injection. If they were to abolish the death penalty based on the overwhelming evidence of racism and class bias, the whole criminal justice system could then be called into question.<p>
But the system has begun to crumble under the weight of its own contradictions. As the facts about the barbarism of lethal injection and the fallibility of sentencing are exposed, pressure to change it is growing throughout society.<p>
This dynamic could lead to a better chance at abolition in Maryland during next year's legislative session. Abolitionists sent an early signal that they won't give up with a demonstration outside the Supermax prison in Baltimore--home of Maryland's death row--on March 24.<p>
With a moratorium still in place and the level of public discussion rising, abolitionists are in an advantageous position to build the movement to shut down Maryland's death row once and for all.<p>

