<font face="Arial, Helvetica, sans-serif" size="2"><b>OBITUARY: TANYA REINHART</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Exposing crimes of the state"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Anthony Arnove</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 30, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">The struggle for Palestinian rights lost an important voice on March 17 with the sudden passing of Israeli author and activist Tanya Reinhart at age 63.<p>
Like her thesis adviser and friend Noam Chomsky, Reinhart combined a life of innovative work in linguistics with a passionate commitment to the struggle against the Israeli occupation, as well as other injustices.<p>
"As all honest people should," Chomsky wrote March 19, "she focused her attention and energy on the actions of her own state and society, for which she shared responsibility--including the responsibility, which she never shirked, to expose crimes of state and to defend the victims of repression, violence, and conquest."<p>
For years, Reinhart risked political isolation for her active involvement in anti-occupation struggles and her courageous writing in the Israeli newspaper <i>Yedioth Aharonoth,</i> which regularly challenged Zionist orthodoxy. Reinhart developed these arguments for international audiences in her books <i><a href="http://www.amazon.com/gp/redirect.html?ie=UTF8&location=http%3A%2F%2Fwww.amazon.com%2FIsrael-Palestine-1948-Open-Media%2Fdp%2F1583226516%3Fie%3DUTF8%26amp%3Bs%3Dbooks%26amp%3Bqid%3D1174796920%26amp%3Bsr%3D8-2&tag=socialistwork-20&linkCode=ur2&camp=1789&creative=9325" target="_blank">Israel/Palestine: How to End the War of 1948</a></i> (Seven Stories) and <i><a href="http://www.amazon.com/gp/redirect.html?ie=UTF8&location=http%3A%2F%2Fwww.amazon.com%2FRoad-Map-Nowhere-Israel-Palestine%2Fdp%2F1844670767%3Fie%3DUTF8%26amp%3Bs%3Dbooks%26amp%3Bqid%3D1174796920%26amp%3Bsr%3D8-1&tag=socialistwork-20&linkCode=ur2&camp=1789&creative=9325" target="_blank">The Road Map to Nowhere: Israel/Palestine Since 2003</a></i> (Verso).<p>
Born in Haifa before the establishment of the state of Israel in 1948, Reinhart was the daughter of a communist mother and herself was active in the Israeli Communist Party and its youth wing. From 1977, Reinhart taught courses in linguistics and literature at Tel Aviv University. For many years, she also taught at Utrecht University in the Netherlands.<p>

