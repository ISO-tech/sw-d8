<font face="Times New Roman, Times, serif" size="3">But in December 2006, Reinhart left Israel and moved to New York to teach at New York University, after being driven from her post at Tel Aviv University.<p>
Reinhart is survived by her husband, the Hebrew-language poet and translator Aharon Shabtai, who has also been an outspoken critic of the Israeli government.<p>
Despite the setbacks she witnessed, Reinhart continued to believe that "persistent struggle can have an effect, and can lead governments to act. Such struggle begins with the Palestinian people, who have withstood years of brutal oppression, and who, through their spirit of <i>zumud</i>--sticking to their land--and daily endurance, organizing and resistance, have managed to keep the Palestinian cause alive, something that not all oppressed nations have managed to do."<p>

