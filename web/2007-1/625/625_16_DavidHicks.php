<font face="Times New Roman, Times, serif" size="5"><b>Tortured in their "war on terror"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 30, 2007 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">AN AUSTRALIAN man detained in the "war on terror" has exposed the U.S. system of torture and abuse at Guant&aacute;namo Bay and other prisons.<p>
David Hicks, the first person to be formally charged by the U.S. under its new tribunal rules, entered a guilty plea March 26 to a charge of providing material support for terrorism, in anticipation of a plea agreement.<p>
According to recently released court documents filed in London, in the five years that Hicks has spent in U.S. custody, he claims he has been subjected to vicious beatings and other illegal actions.<p>
Hicks maintains that after his capture in Afghanistan in 2001, American guards threw him and other detainees on the ground and walked on their bodies. He also says that he was stripped naked, had all of his body hair shaved and had an object inserted into his rectum by guards.<p>
Describing being interrogated by Americans dressed in black combat gear without any insignia, Hicks claims that one interrogator, "obviously agitated, took out his pistol and aimed it at me, with his hand shaking violently with rage." "I realized that if I did not cooperate with U.S. interrogators, I might be shot," Hicks said.<p>
Hicks was brought to the U.S. prison camp in Guant&aacute;namo Bay, Cuba, in 2002--where he says he was subject to repeated, forced injections and he witnessed other detainees being mistreated. One detainee, a man with one leg, was dragged out of his cell, after being attacked by a prison dog, Hicks said.<p>
The U.S. military denies that any such abuse ever took place. It claims that Hicks is lying, though other detainees have reported similar treatment.<p>
The U.S. is also targeting Hicks' military lawyer, Maj. Michael Mori.<p>
Mori, who has mounted an aggressive campaign in Hicks' defense, was recently accused by Col. Morris Davis, the chief prosecutor of the military commissions, of violating Article 88 of the Uniformed Code of Military Justice--which prohibits an officer from using "contemptuous words" against the president, vice president, secretary of defense and other government officials.<p>
According to the <i>New York Times</i>, in a complaint to the judge who oversees military commissions, Davis claims Mori made statements that could be considered insulting or rude while on trips to Australia to raise support for Hicks.<p>
In other words, "enhanced interrogation techniques" like waterboarding and other forms of torture are acceptable in the eyes of the Bush administration--but a lawyer pointing out such illegal and inhumane treatment and laying the blame at the feet of administration officials is a punishable offense.<p>

