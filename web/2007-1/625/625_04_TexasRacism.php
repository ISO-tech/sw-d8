<font face="Times New Roman, Times, serif" size="5"><b>Racism on display in a Texas town</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Cindy Berringer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 30, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">A 14-year-old African American youth is serving a possible seven-year prison sentence in the vicious Texas "justice" system--for the "crime" of shoving a school hall monitor.<p>
Shaquanda Cotton is accused of pushing a school aide who tried to prevent her from entering Shaquanda's school in Paris, Texas, before the first bell. Shaquanda maintains that she had permission to enter the school early, and that the aide pushed her first.<p>
Despite having no prior arrests, Shaquanda was sent away to a youth prison for a sentence that could run until she is 21.<p>
She has tried to harm herself three times in her cell, including trying to cut off her breathing by wrapping a sweater around her neck. "Sometimes I feel that I just can't do this no more--that I can't survive this," she says.<p>
This case and its absurd sentence simmered quietly for more than a year until Howard Witt of the <i>Chicago Tribune </i>picked up the story from a Houston tabloid and wrote an article published March 12.<p>
With almost no help from the official media, Shaquanda's story spread like a Texas wildfire through the Internet and talk radio. Paris officials are looking ridiculous as they scramble to claim that the small community isn't racist.<p>
According to the <i>Tribune</i>, Shaquanda's mother, Creola Cotton, feels her daughter was targeted by the school system. Creola Cotton had frequently charged the school with racism, and had led a protest march in front of a school. The school is under continuing investigation by the Department of Education for allegations of racism.<p>
The <i>Tribune </i>article pointed out that a century ago, the biggest spectacle for white citizens at the public fairgrounds in Paris was watching lynchings of Blacks.<p>
While a local civil rights activist compared Shaquanda's sentence to the 1930s, others might be reminded of the 1960s when unwelcome "outside agitators" were targeted for stirring up unwanted trouble.<p>
Phillip Hamilton of <i>The Paris News</i> blames "outside influences" and "recent out-of-town guests" for the town's troubles--such as a march outside the county courthouse and the school administration building led by the New Black Panther Party. Hamilton called the <i>Tribune </i>article "a journalistic lynching" full of "false statements, omitted facts and inaccurate information"--none of which he adequately refutes.<p>
The judge who sentenced Shaquanda to prison, Chuck Superville, says that Shaquanda had been a "persistent behavior problem at school."<p>
The <i>Tribune </i>article lists her disciplinary citations at school as "wearing a skirt that was an inch too short, pouring too much paint into a cup during an art class and defacing a desk that school officials later conceded bore no signs of damage." Superville neither disputes these citations, nor lists any more damning.<p>
Probation was not an option, says Superville, because "the mother failed to cooperate at every turn...and I must determine whether or not it is in the child's best interest to be removed from the home."<p>
If Shaquanda had been white, though, things might have turned out differently. Three months before Shaquanda was tried, Superville presided over the trial of a 14-year-old white girl who was convicted of arson for burning down her family's house. Superville sentenced her to probation.<p>

