<font face="Times New Roman, Times, serif" size="3"><p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">AS POINTED out by <i>Socialist Worker</i>, the American execution system is grinding to a halt. The number of executions is dropping. And because the public is learning more about the death penalty, juries are rebutting bloodthirsty prosecutors by rejecting death sentences.<p>
We closed the door to executing juveniles and the mentally retarded, and now, doctors and anesthesiologists, who take an oath to preserve life, are refusing to participate in the barbaric process of killing human beings. As in the past with the electric chair, gas chamber and hangings, lethal injection is now under fire for being nothing more than another form of state-sanctioned torture.<p>
Because of this and much more, the death penalty is on hold in many states. Some states are even considering abolishing it altogether.<p>
I'm excited that the tide is turning on executions, but I'm disappointed that the fact of it being flat-out racist has gotten lost in the debate.<p>
Racism in the U.S. is as American as apple pie. It touches every aspect of Black life--education, housing, health care, employment, the criminal justice system and its machinery of death.<p>
It can be blatant, like when President Bush sat idly by while thousands of Blacks suffered and screamed for help during the Hurricane Katrina catastrophe--and Bush then publicly praised the head of FEMA for "doing a fabulous job." Or it can be hidden, like when former Supreme Court Chief Justice William Rehnquist purchased his Arizona home under a signed contract that he would "never sell the property to a Black person."<p>
But we cannot escape this obvious plague on society and humanity by ignoring its existence.<p>
"The criminal justice issue," said Rev. Jesse Jackson and Minister Louis Farrakhan during a historic November 2004 meeting, "is the most major civil rights issue of our time that must be addressed."<p>
We know that whites commit more crimes and more murders and use more drugs in this country, but Blacks are being targeted and persecuted on a larger scale. The weight of the police, courts and prison system falls more heavily on Blacks than any other group.<p>
Blacks are 12 percent of the U.S. population, but are 41 percent of its prison population. Almost one in three Black men can expect to spend time in prison at some point in their lives--compared to about one in 25 white men.<p>
I've been incarcerated for over 22 years in the state of Illinois, and in all the prisons I've been in (Pontiac, Menard, Stateville, Galesburg and Western Illinois), they were and are overwhelmingly packed with Black men on a much larger scale than the national average.<p>
The U.S. Supreme Court banned capital punishment in 1972 on the basis that it was being carried out in an arbitrary and capricious manner. The court thought the problem could be rectified when it reinstated the death penalty in 1976. But here we are 35 years later, dealing with the same racist and unfair system of death.<p>
Unfortunately, today's Supreme Court refuses to consider the statistical facts as the 1972 court did. Today's court insists that prisoners prove they were discriminated against as an individual, and not as a part of the whole--which is virtually impossible.<p>
We cannot depend on the courts and government to end racist practices when they are part of the problem. We did not wait on the courts, the government or popular opinion to change in order to demand an end to slavery, segregation, Jim Crow laws or flying the Confederate flag. So we do not have to wait to demand an end to the legal lynchings going on in concentration camps across this nation.<p>
If the criminal justice system is the "most major civil rights issue of our time," then there is no difference between the lynchers of 14-year-old Emmitt Till in 1955 and a racist government and society that continues to lynch Blacks today, in a different way.<p>
America has come a long way since the fight for civil rights, but everyone knows that we have a long way yet to go. And knowing that we're living under this cloud of racism, we must not allow them to take it out of the debate. Regardless of polls and popular opinion, the racist death penalty must go.<p>

