<font face="Times New Roman, Times, serif" size="5"><b>The legal lynchings that still go on today</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 30, 2007 | Page 5</font><p>

<font face="Times New Roman, Times, serif" size="3">STANLEY HOWARD is a former death row prisoner who was pardoned by then-Illinois Gov. George Ryan in 2003.<p>
Stanley landed on death row because of the torturers of the Chicago Police Department, who suffocated him with a typewriter cover while continuously beating him until he confessed to a crime he didn't commit.<p>
In prison, Stanley led the way in forming the Death Row 10--a group of condemned prisoners who all endured similar police torture. Their struggle from behind prison bars played an important part in building pressure on Ryan to clear death row.<p>
Still in prison for another crime he was falsely accused of, Stanley continues to speak out against the death penalty and the criminal justice system, writing regularly for the Campaign to End the Death Penalty's newsletter the <i>New Abolitionist</i>.<p>
In this article for <i>Socialist Worker, </i>Stanley looks at the latest developments in the battle against the death penalty.<p>

