<p><a name="Wilson"></a><font face="Times New Roman, Times, serif" size="4"><b>Free Genarlow Wilson</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IN YET another blatantly racist case, Genarlow Wilson, a high school senior in Douglas County, Georgia, has been behind bars for the last two years and will stay there for the next eight.<p>
He was convicted of aggravated child molestation for a voluntary act of oral sex with another teenager. He was 17, and she was 15--three weeks from her 16th birthday. The age of consent in Georgia is 16. To put this in context, according to the Center for Disease Control and Prevention, approximately 47 percent of 9th through 12th graders have already had sex.<p>
The most disgusting element of the whole rotten case is that, according to the NAACP, a white defendant was recently convicted and sentenced to only 90 days for having sex with a younger child. Compare this to the mandatory sentencing of Genarlow Wilson--who is Black--to 10 years.<p>
How many more Black men need to waste their life because of the criminal injustice system?<br>
<b>Roberto Rosario</b>, New York City<p>
