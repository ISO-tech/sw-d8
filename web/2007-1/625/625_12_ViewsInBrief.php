<a name="Nazis"></a><font face="Times New Roman, Times, serif" size="4"><b>Neo-Nazis target immigrants</b></font><p>

<font face="Times New Roman, Times, serif" size="3">"IT IS domestic terrorism. It always has been domestic terrorism, and it always will be domestic terrorism until it stops." So testified police detective Matt Browning at a recent hearing at the Arizona statehouse about anti-immigrant vigilantism on the Arizona border with Mexico.<p>
Browning has spent 12 years working undercover in various vigilante groups and confirmed what immigrant rights' activists have argued all along: that "protecting the border" is a cover for neo-Nazi and white-supremacist groups to organize and recruit.<p>
Browning described the three border militia groups and six white-supremacist groups he worked with as some of "the most violent, racist skinheads in the nation." He recalled that almost every meeting of these groups he attended over 12 years dealt with immigration.<p>
Mark Pitcavage, the national director of research for the Anti-Defamation League, drew a distinction in his testimony between these hate groups and vigilante organizations like the Minutemen. But he confirmed that on actual border patrol operations, members of both groups mingle with and feed off one another.<p>
Both speakers stressed that the rhetoric of vigilante and supremacist groups leads to the kind of violence that Steve Boggs perpetrated in 2005.<p>
Boggs murdered two Latinos and an American Indian man at a Jack in the Box restaurant in Mesa--some 200 hundred miles from the border. Boggs had been involved with a white-supremacist militia and had written a letter to the police, in which he stated he wanted to "rid the world of a few needless illegals."<p>
It is often argued that we shouldn't focus so much on the actions and hate speech of racists like Boggs--that the attention only helps this tiny minority to grow. But when federal officials conduct random sweeps and workplace raids, as they did recently in New Bedford, Mass., the atmosphere grows only more rank for racist vigilantes to turn their rhetoric into action.<p>
That is why we need to build a movement for immigrants to stop the raids by ICE officials <i>and</i> confront the racists head-on.<br>
<b>Jeff Bale</b>, Phoenix<p>
