<p><a name="Chavez"></a><font face="Times New Roman, Times, serif" size="4"><b>Ch&aacute;vez and struggle from below</b></font><p>

<font face="Times New Roman, Times, serif" size="3">LEE SUSTAR'S article on Hugo Ch&aacute;vez and Venezuela gives a sharp and balanced view of a highly complex and rapidly developing situation ("The struggle in Ch&aacute;vez's Venezuela," March 16). There are a few things however that I would like to add, mostly in reference to the proposed united socialist party of Venezuela (PSUV).<p>
First of all, there will be nothing obligatory about joining or merging with the new party. Other political parties of both the right and left will continue to exist.<p>
Significantly, Ch&aacute;vez has emphasized that the new party must have profound internal democracy with party leaders directly accountable to rank-and-file members and that the party's platforms will be voted on by a referendum of party members.<p>
This has led the center-left social democratic parties, "Father Land for All" and "We Can" (PPT and PODEMOS) along with the Venezuelan Communist Party (PCV)--all three of which have campaigned heavily for Ch&aacute;vez--to announce that they will not join the new party.<p>
The careerist political elites who operate these parties are hostile to Ch&aacute;vez's proposal because they fear they will lose their privileged and unaccountable positions. If they change their decision not to join the PSUV in the future it will only be out of that same fear.<p>
Meanwhile, Trotskyist union leaders like Orlando Chirino, Stalin Perez and their CCURA tendency within the National Union of Workers (UNT) have joined the discussion process for the creation of the PSUV.<p>
Chirino's tendency in the UNT has remained independent and become the largest tendency, in spite of the UNT being full of bureaucrats and reformists. They would join the PSUV only on the basis of remaining autonomous as an internal tendency.<p>
Of course, revolutionaries should not count their chickens before they are hatched by putting too much stock in this new party before it has formed, but nor should we be overly pessimistic. For all these reasons, the idea that Ch&aacute;vez is trying to establish a "one-party state" must be rejected outright.<p>
The added power granted to Ch&aacute;vez by the National Assembly in the form of a presidential decree is another issue. As socialists we should be critical thinkers who do not gloss over complex questions, but that of course means looking at both sides.<p>
The decree grants Ch&aacute;vez the very specific ability to pass certain types of laws. Laws passed by decree can in fact be revised and vetoed by the National Assembly, unlike the fast-track legislation powers granted to Bush. Also laws passed by decree--like any under Venezuela's 1999 constitution--can be overturned by popular referendum. So while the presidential decree may not be new to Venezuela, the people's ability to check it is.<p>
Of course, we must always be clear that genuine socialism cannot be handed down by decree, but can only be won by struggle from below. We should remain critical while recognizing that Ch&aacute;vez has not yet shown any signs of blocking struggles from below--in fact, quite the opposite.<br>
<b>Zach Mason</b>, Washington, D.C.<p>
