<font face="Times New Roman, Times, serif" size="5"><b>Sparta's war on terror</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Matt Korn</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 30, 2007 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i><a href="http://300themovie.warnerbros.com/" target="_blank">300</a>, </b></i>written and directed by Zack Snyder, based on the graphic novel by Frank Miller, starring Gerard Butler and Lena Headey.</font><p>

<font face="Times New Roman, Times, serif" size="3">FRANK MILLER'S <i>300</i> has all the elements to make it a runaway box office success. Massive violence, gore, a sex scene in the first 15 minutes and hyper-stylized cinematography that makes you feel like you're playing a videogame.<p>
It's also a rank cesspool of racism, sexism, homophobia and "freedom-loving" pro-war propaganda.<p>
<i>300</i>'s style, pro-war message and racism evoke Sergei Eisenstein's classic propaganda film <i>Alexander Nevsky</i>, a retelling of Russia's defense against the Holy Roman Empire, which was used as blatant pro-war propaganda in Russia in the lead-up to Second World War.<p>
But where <i>Nevsky</i> was artistically groundbreaking, and made by a man whose previous work was revolutionary in every sense of the word, <i>300</i> rehashes the same art direction as Miller's previous film <i>Sin City</i> and continues to spew the right-wing politics of its creator.<p>
The movie <i>300</i> inaccurately follows the Battle of Thermopylae, a sort of Alamo for ancient Greece, where an army of about 4,000 Greeks--including 300 Spartans--faced a much larger army of Persians, commanded by King Xerxes. The eventual defeat of this small force is said to have rallied the rest of Greece for a final victory against the Persians.<p>
The film is riddled with historical inaccuracies, but it's the ones that send clear right-wing messages that are the most alarming. The filmmakers go to great lengths to portray the Persians as evil, decadent, cruel and effeminate, unlike the manly men of Sparta.<p>
The Persians employ a whole host of horrifying creatures, from deranged giants to a monster with bone-axes for arms, in contrast to the flawless chiseled bodies of the Greeks. Inexplicably, the Persians are mostly played by Black actors, or actors wearing dark body paint, going up against white Greeks, played by British actors.<p>
Over and over again it is mentioned that the Persians fight with a slave army, and run a vast empire on slave labor. Not even once is it mentioned that all of Greek society, especially Sparta, kept slaves in extremely cruel bondage.<p>
The incessant narration (that insists on describing every moment of the movie) points out that the Spartan force has some "freed slaves" in it, without pointing out that they had to be slaves first to be freed.<br>
Women in the film are mainly used as sex objects. The one powerful woman, Queen Gorgo of Sparta, ends up being raped by Theron, a Spartan politician who's bribed by the Persians to resist sending the entire Spartan army into battle.<p>
The one satisfying moment of the film is when Gorgo gets her revenge by stabbing Theron and exposing his treason.<p>
Homophobia gets its due as well in <i>300</i>, with the Persians--especially Xerxes--portrayed as effeminate and cruel. In one scene, the giant Xerxes implies that King Leonidas of Sparta will become his personal sex slave after the Spartans' defeat.<p>
The most dangerous aspect of <i>300</i> is its blatant call for the West to attack Iran. Iran, after all, used to be called Persia, and the film pulls no punches in exhorting the "free and rational" West to defend itself against the Persian hordes. Queen Gorgo even utters the tired clich&eacute; "Freedom is not free!"<p>
"By ancient Persia, they refer to modern Iran--whose soldiers are portrayed as bloodthirsty, underdeveloped zombies," points out Greek film critic Dimitris Danikas. "They are stroking racist instincts in Europe and America."<p>
It shouldn't be surprising then that Miller's next project is a comic book called <i>Holy Terror, Batman!</i> where the Caped Crusader goes up against Al-Qaeda in New York City.<p>
Film critic Curt Holman best sums up why a film like <i>300</i> is so dangerous in today's political climate: "You can easily imagine <i>300</i> being used as the best military recruitment film ever, lacking only a coda like, 'Did you know that Persia is now called Iran? Let's invade Tehran and kick ass like Spartans!' In the wrong hands, <i>300</i> could be a lethal weapon."<p>

