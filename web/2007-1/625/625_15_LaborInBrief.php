<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">March 30, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Philadelphia Community College</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Matthew Richman and Lee Sustar</b></font><p>

<font face="Times New Roman, Times, serif" size="3">PHILADELPHIA--The union representing staff and faculty at the Community College of Philadelphia (CCP) reached a tentative agreement March 25 to end a two-week strike.<p>
The union, American Federation of Teachers Local 2026, walked out over the administration's steep proposed cuts in health care and low salary and wage increases.<p>
According to union co-president John Braxton, the administration backed off its demands to force workers to pay for health insurance premiums. CCP also improved its wage offer. The deal includes wage increases of 3 percent in each year of the five-year contract, and an immediate 3.75 percent wage increase.<p>
"I feel good that we've come back with a package that has more money in it than what we left the table with two weeks ago [when the union went on strike]," Braxton told the <i>Philadelphia Daily News.</i> "To me, that's a victory."<p>
Under the old contract, CCP had among the lowest wages for faculty and staff for comparable institutions in the Philadelphia region.<p>
While the administration asked workers to tighten their belts, CCP--a public institution--refused to reveal administrative salaries and costs, according to the union. But CCP has launched a $1.5 million "branding campaign" and has increased the number of highly paid administrators by 20 percent in the past four years.<p>
Cynthia Reid, an administrative assistant and the co-chair of the classified workers union, said that there are workers she represents who make less than $16,000 per year, while CCP President Steven Curtis makes $185,000 per year, according to the <i>Philadelphia Inquirer</i>. "If we're in a crunch, how come he can't take a pay cut?" Reid wondered.<p>
Many students at CCP come from union families or are union members themselves. Reid told <i>Socialist Worker</i> that the student government has supported the workers against the administration, especially since CCP has refused to add enough classes to meet student demand.<p>
The CCP administration's actions represent a broader trend in changes to the structure of colleges and universities in the United States, using corporate models for education--focusing on the bottom line instead of the needs of workers and students.<p>

