<p><a name="Imm"></a><font face="Times New Roman, Times, serif" size="4"><b>Rallying for immigrant rights</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By David Thurston</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WASHINGTON--Approximately 5,000 people rallied for immigrant rights on the west lawn of the Capitol Building June 2.<p>
The National Capital Immigration Coalition (NCIC) organized the event as a way to influence the congressional debate on immigration, currently dominated by the "compromise" proposal introduced in the Senate.<p>
Speakers criticized many components of the Senate compromise, including $5,000 fines for undocumented workers, the attempt to abolish a family-based immigration system, and provisions requiring immigrants who want citizenship to leave the U.S. in order to apply.<p>
"Legalization without an exact date is unacceptable," said NCIC President Jamie Contreras. "A guest-worker program that creates a permanent underclass of workers is unacceptable."<p>
But the organizers' goal was not to defeat the Senate bill. The rally didn't include any signs calling for legalization for all, and one of the main slogans was "Comprehensive immigration reform now"--a phrase used by everyone from George Bush to the main sponsors of the Senate bill and similar legislation that is pending in the House.<p>
The framework being advanced by these forces is one in which intensified enforcement of immigration law and some form of a guest-worker program are delivered in exchange for a torturous legalization process for a portion of the undocumented population.<p>
The turnout for this demonstration shows that thousands are ready to mobilize to defend immigrant rights and demand a just immigration policy--but to advance that sentiment, we need to reject the terms of debate in Congress and demand full legalization for all.<p>
