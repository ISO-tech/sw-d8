<p><a name="Hayes"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend Lawrence Hayes</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Ben Davis and Rebecca Kurti</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Anti-death penalty activists are rallying against the most recent round of intimidation against former death row prisoner Lawrence Hayes.<p>
Hayes, a former Black Panther and founding member of the Campaign to End the Death Penalty (CEDP), was recently re-imprisoned after being accused of a parole violation.<p>
In support of Hayes, the CEDP staged a press conference in front of the parole office in Brooklyn on May 25. In attendance were representatives from New Yorkers Against the Death Penalty and the anti-Lynching Movement; Councilman Charles Barron; Rev. Earl Koopercamp of St. Mary's Church in Harlem; Alan Newton, an exonerated New York prisoner who has been touring with Hayes and speaking about wrongful incarceration; and a coworker from the social service agency where Hayes is employed.<p>
In the past, Hayes spent nearly three years behind bars on two separate parole violations that later proved to be false. These violations came within days of public appearances during which he spoke out against the death penalty.<p>
While parole wreaks havoc on his life, Hayes has continued to make positive contributions to the community through his social service work. Activists are working to continue to keep Hayes' case front and center, and win justice for this outspoken fighter against the criminal injustice system.<p>
