<a name="ICE"></a><font face="Times New Roman, Times, serif" size="4"><b>ICE raid at UC-Santa Barbara</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Owen Goodwin</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SANTA BARBARA, Calif.--University of California (UC) students are expressing concern and anger following an Immigration and Customs Enforcement (ICE) raid and the arrest of a student who attends UC-Santa Barbara.<p>
The raid happened in the early morning hours of May 23 when ICE agents entered an Iranian student's apartment to check her papers. Finding no problems, they turned to the woman's roommate--Yoon Choi, a third year sociology and philosophy major, who had an expired visa. Choi was arrested and is currently being held in a San Pedro jail, awaiting a hearing with an immigration judge.<p>
The raid has left undocumented students throughout the UC system feeling vulnerable to arrest.<p>
According to the UCSB paper, the <i>Daily Nexus</i>, schools are required to verify the immigration status of international students through a system known as Student and Exchange Visitor Information System. But ICE apparently skipped any communication with the school before the raid, which is why agents mistakenly went to the Iranian students' apartment in the first place.<p>
ICE is obviously employing the same tactics it has used in raids across the country--in which bystanders have frequently been swept up.<p>
This incident also follows a string of racist injustices at the University of California. In the winter, an Iranian at UCLA was tasered in the library when he protested being racially profiled by campus police. At UC-Santa Cruz, administrators have singled out an African-American student, Alette Kendrick, sentencing her to an effective expulsion for her part in a campus protest last fall.<p>
Students need to challenge this latest attack on our schoolmates--and demand an end to the ICE raids now.<p>
