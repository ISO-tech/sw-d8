<p><a name="Memorial"></a><font face="Times New Roman, Times, serif" size="4"><b>Antiwar protest on Memorial Day</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Ken Love</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ROCHESTER, N.Y.--On Memorial Day, members of Rochester Against War (RAW), along with several other groups, filled a city block to march directly behind the city's yearly Memorial Day parade.<p>
Carrying placards, banners and three 100-foot-long strands of rope with all the names of U.S. soldiers killed in the war attached, about 110 antiwar activists marched in the contingent.<p>
Jake Allen, an organizer at his high school Campus Antiwar Network chapter, played bagpipes to keep the cadence as the group marched forward. "I think support has certainly grown for our movement," he said, "but I think that it still remains the silent majority. There were very few who were openly against what we were doing...[M]ost people, when they realized what was happening, applauded in support, and some folks even yelled out, 'Bring them home now!'"<p>
At one point at the center of downtown, the entire sidewalk erupted in loud applause and messages of approval after an activist yelled out, "Make some noise if you're with the antiwar majority!"<p>
Activists from RAW were able to distribute flyers and literature about the new Antiwar Storefront and to build for a July 3 "Cut the Crap, Cut the Funds!" protest that's being planned to give expression to the widespread discontent with the Democratic Party's inability to listen to the November mandate against the war.<p>

