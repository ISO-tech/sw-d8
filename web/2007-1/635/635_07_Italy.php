<font face="Times New Roman, Times, serif" size="3">One major source of anger for many Italians is the U.S. military presence in Italy itself. There are nine U.S. bases throughout the country, including one near Vicenza in the northeast that is particularly important to U.S. war strategy in Iraq and Afghanistan.<p>
Yet another focus for Italian disgust with Bush will become evident a few days after his visit, when the trial of Mario Lozano resumes in Rome. Lozano is the U.S. soldier who in March 2005 opened fire on a car carrying journalist Giuliana Sgrena to the Baghdad airport after she was released by guerrillas who had kidnapped her.<p>
Sgrena was severely wounded in the shooting, and the Italian secret service agent who negotiated her release, Nicola Calipari, was killed. An Italian court has summoned Lozano to stand trial in Rome on charges of voluntary political homicide. But Lozano scoffed at the idea that he should be held responsible, and has publicly attacked Sgrena as a dangerous radical out to get him.<p>
So many Italians are outraged by Bush's visit, but it appears that the anti-Bush protest may be split by the current pressures of Italian electoral politics. Antiwar activists and independent union militants are planning a march through the center of Rome demanding an end to the wars in Iraq and Afghanistan and an end to the Prodi government's support for U.S. imperialism everywhere.<p>
But left-wing political parties and unions who are part of Prodi's coalition want to confine the protest to Bush's Iraq policy. They plan a "sit-in" demonstration separate from the march.<p>
Efforts to forge a united front to denounce Bush are ongoing. Whether there are two protests or one, Bush will leave Italy knowing that a majority of Italians reject his politics of war and exploitation.<p>

