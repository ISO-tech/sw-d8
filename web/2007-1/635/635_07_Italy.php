<font face="Times New Roman, Times, serif" size="5"><b>The protests awaiting Bush in Italy</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 8, 2007 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>BILL KEACH reports from Italy on plans to greet George Bush with demonstrations when he visits there June 9.</b><p>

GEORGE BUSH can expect a hot antiwar protest when he comes to Rome on June 9 following his trip to the Group of Eight summit in Germany. Thousands of people from all over Italy are coming to Rome to express their opposition to Bush's wars in Iraq and Afghanistan--and to their own government's complicity in Bush's policies.<p>
Opposition to Bush's visit is so intense that several pro-American politicians have written to ask that it be canceled. The Italian Interior Ministry has issued a statement vaguely hinting at concerns about political violence by protesters.<p>
According to the official White House press release, the highlight of Bush's visit to Rome will be a meeting with Pope Benedict XVI, a reactionary whose hypocritical opposition to gay rights and all forms of civil union has provoked widespread anger.<p>
Bush's visit comes at a moment of crisis for the center-left coalition government headed by Prime Minister Romano Prodi. The government was thrown into turmoil earlier this spring over a vote to discontinue Italian participation in the Afghanistan war.<p>
Recent local elections demonstrate that Prodi's current government is weaker and less popular than ever. Its support comes mainly from the fear that Bush's friend, right-wing media tycoon and former Prime Minister Silvio Berlusconi, will return to power.<p>

