<font face="Times New Roman, Times, serif" size="5"><b>ICE is "trying to scare the community"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 8, 2007 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>ALVARO LOPEZ reports on the aftermath of an immigration raid in Brooklyn.</b><p>

THIRTEEN GUATEMALAN immigrants are the victims this time in yet another Immigration and Customs Enforcement (ICE) raid in New York City.<p>
On May 20, about a dozen ICE agents rushed into a Brooklyn apartment, allegedly looking for an "Oscar." Nobody with that name was found, but agents demanded that everyone in the apartment present valid identification. Of the 15 people in the apartment, all but Obispo Lopez and his 16-year-old son Nehemias were detained.<p>
As Lopez told the <i>El Diario </i>newspaper, "They asked us for our papers, and I showed them my identification from the Guatemalan consulate, and the others did as well, but they took them anyway. They didn't even let them take anything or get dressed."<p>
Lopez, a construction worker, says the agents took away two of his sons, Evijoel and Avimail, and his son-in law, Alvaro Juarez. As of now, there is no word where the men are being held, but the family has received a phone call saying that they are in good condition.<p>
The day after the raid in the largely immigrant neighborhood of Bensonhurst, people were concerned for the fate of the detainees, and for their own safety. ICE officials say the raids are designed to detain "criminals," or those who have ignored final deportation notices. But the May 20 raid isn't an isolated one.<p>
"This is the third time there's been a raid in this neighborhood," Benny Hernandez, who owns a small deli across the street from the apartment where the Guatemalan immigrants were detained, said in an interview. "The raid before this, they left children behind and deported one of the parents.<p>
"They're attempting to scare the community...People who just want to work will be terrified to do that."<p>
Others in the community know firsthand what it is like to have their lives disrupted. A mother of two said she remembered vividly a raid four months ago, in which the husband of a close friend was detained. Once again, ICE agents were looking for someone who either didn't exist or no longer lived in the apartment. But five people in total were detained and deported back to Ecuador.<p>
"Her husband is in Ecuador now, and they don't know what to do," the woman said. "I'm undocumented too, and I think it's unjust to separate families. I live very close by, and I'm worried about my children. I don't have any family here, so what happens if they take me? Who will take care of my children?"<p>
Stories like these will only multiply as federal immigration authorities attempt to reach their goal of deporting 75,000 immigrants by the end of this year.<p>
"The government has to be responsible for its actions," says Maribel Lopez, an immigrant mother of two who also lives near the site of the Brooklyn raid. "I've seen people die of hunger in my native country. People come here to seek a better life and to see their children succeed, but this [proposed immigration legislation under consideration in the Senate] is unjust and doesn't allow for us to settle."<p>
Currently, Obispo Lopez and Brooklyn Pastor Erick Salgado are working with the ACLU in New York to get legal services for the workers detained in Brooklyn and their families back in Guatemala. We need to continue building a grassroots response to the ICE raids, and demand an end to the deportations and a just immigration policy.<p>
<i>Luisa Gallego contributed to this report</i>.<p>

