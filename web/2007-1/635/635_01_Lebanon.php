<font face="Times New Roman, Times, serif" size="5"><b>Lebanese Army pounds refugee camps</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 8, 2007 | Pages 1 and 8</font><p>

<font face="Times New Roman, Times, serif" size="3">THE LEBANESE Army intensified its assault on a Palestinian refugee camp in northern Lebanon following the breakdown of a 10-day ceasefire in early June--and exchanges of gunfire at another camp in the south raised fears that the two-week-old conflict would spread.<p>
Some 25,000 of the 40,000 people living in the Nahr al-Bared camp in the north fled the fighting, sparking a crisis in the nearby Badawi camp, whose population swelled from 15,000 to 28,000 in a matter of days.<p>
The official death toll in the army attack reached 110 on June 3, but the real figure is no doubt higher, and will climb as more bodies are uncovered from the rubble.<p>
"After three days of shelling and more than 100 dead and with no electricity or water," wrote American eyewitness Franklin Lamb on the Palestine Chronicle Web site, "Nahr al-Bared reeks of burned and rotting flesh, charred houses with smoldering contents, raw sewage and the acrid smell of exploded mortars and tank rounds."<p>
The Lebanese government justified its ruthless assault on the camp by insisting that it is attacking armed militants from the Fatah al-Islam, which is affiliated to the al-Qaeda network. Echoing arguments used by the U.S. and Israel, it claimed that civilian deaths and damage to infrastructure were thus a regrettable but necessary cost of "rooting out the terrorists."<p>
But according to investigative journalist Seymour Hersh, as well as other Lebanon experts, Fatah al-Islam, which began as a splinter of some 200 people from another militia last fall, has some unlikely friends--namely, the U.S. and the U.S.-backed Lebanese government.<p>
Speaking to CNN International in late May, Hersh described "a private agreement" between Dick Cheney and Elliot Abrams, and Saudi Arabia's Prince Bandar bin Sultan.<p>
"The idea was to get support--covert support and money--from the Saudis to support various hard-line, jihadist Sunni groups, particularly in Lebanon, who would be seen in case of an actual confrontation with Hezbollah, the Shia group in the southern Lebanon, as an asset," Hersh said.<p>
But Fatah al-Islam--like the Afghan mujahideen that the U.S. supported against the USSR in the 1980s--took money and weapons from its Lebanese and American sponsors, and then began pursuing its own agenda.<p>
The Palestinian refugee population in Lebanon is paying the price--even though most condemn Fatah al-Islam as an outside group that has nothing to do with the Palestinian cause. In fact, Fatah al-Islam's leader is Palestinian, but the bulk of the group's members are from Saudi Arabia, Syria, Pakistan, Algeria and elsewhere.<p>
But the Lebanese government is playing up anti-Palestinian sentiment as part of its justification for the military's brutality.<p>
More than 200,000 Palestinians live in a dozen squalid UN-administered refugee camps throughout Lebanon, and another 200,000 Palestinians live outside the camps. This population is the result of the establishment of the state of Israel in 1948. To this day, Palestinians live in Lebanon as second-class citizens, caught in a legal limbo as they hope to return to homes and land they fled 60 years ago.<p>
Palestinians are banned from working in some 70 professions, denied access to social services and barred from owning real estate. As a result, they are almost entirely dependent on the UN-administered refugee programs for all their basic needs.<p>
This has made it easy to foment divisions between Palestinian and Lebanese--and is part and parcel of a broader U.S. divide-and-conquer strategy in the Middle East.<p>
According to Hersh, "The American role is very simple right now. Condoleezza Rice, the secretary of state, has been very articulate about it. We are in the business now of supporting the Sunnis anywhere we can against the Shia--against the Shia in Iran, against the Shia in Lebanon...<p>
"The Arabic word is fitna--civil war. We are in the business right now of creating in some places--Lebanon in particular--sectarian violence."<p>

