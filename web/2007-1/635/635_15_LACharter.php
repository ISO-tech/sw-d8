<font face="Times New Roman, Times, serif" size="5"><b>Setback for LA charter scheme</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 8, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>UTLA member RANDY CHILDS reports on the battle over the future of Locke High School.</b><p>

LOS ANGELES--Green Dot Public Schools, the charter school operator that's trying to take over Locke High School in South Central LA, was riding high a few weeks ago.<p>
It had announced that a majority of Locke's permanent teaching staff had signed petitions supporting a conversion of the struggling school to a charter run by Green Dot. The union-bashing editorial writers of the <i>Los Angeles Times </i>said the Locke petition was a sign that teachers are "perfectly willing to loosen work rules and toss tenure out the classroom window if it means a safer and more vibrant campus."<p>
Or not.<p>
Officials of the Los Angeles Unified School District (LAUSD) announced that 17 of the 41 teachers who originally signed Green Dot's petition have since rescinded their support of the charter plan, saying that petition gatherers didn't give them all the information.<p>
Math teacher Zeus Cobias, for example, said many Locke teachers signed the petition because they "feel like there isn't any other option" to save the school. History teacher Frank Wiley said he was desperate to see improvements at the school and hoped the petition might bring changes, but had no intention of becoming a Green Dot employee.<p>
After Green Dot made its initial announcement, LAUSD officials and leaders of United Teachers Los Angeles (UTLA), the union representing the city's 48,000 teachers, each had closed-door meetings with the Locke faculty.<p>
According to Joel Jordan, a leading UTLA staff member present at both meetings, district officials made unconvincing promises of reform and greater autonomy for the school. But UTLA President A.J. Duffy blasted the LAUSD bureaucracy and told teachers he thought they were right to fight for local control of their school.<p>
Green Dot's plans for Locke include forcing every teacher to reapply for their job, slashing teachers' health and retirement benefits, and eliminating seniority and work rule protections currently guaranteed by the UTLA contract.<p>
If that isn't bad enough, Green Dot CEO Steve Barr has admitted publicly that he plans to discriminate against experienced teachers whose high salaries he supposedly "cannot afford."<p>
Many Locke teachers seem to have gotten UTLA's message--which includes an invitation to fight for smaller class sizes, local control and more money for the classroom, without sacrificing the hard-fought teachers' rights guaranteed in our union contract. That's the way to change the rotten state of LAUSD.<p>

