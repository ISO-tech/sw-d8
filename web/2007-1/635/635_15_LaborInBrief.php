<a name="UC"></a><font face="Times New Roman, Times, serif" size="4"><b>University of California custodians</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Kathryn Lybarger, AFSCME Local 3299</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AFTER AN 18-month fight, custodians on three campuses of the University of California (UC) system--Berkeley, Santa Cruz and Santa Barbara--declared victory on May 22.<p>
Represented by AFSCME Local 3299, the custodians forced UC to bring their wages close to par with what custodians earn at comparable institutions. Management finally agreed to a $1.75 an hour increase for custodians on the three campuses--almost a 20 percent increase for some--plus a 50 cent an hour increase for custodians at all other UC campuses and medical centers.<p>
In addition, groundskeepers at UC-Irvine won their struggle to be represented by the union, and all workers represented by AFSCME that make less than $40,000 a year will see a small percentage increase in their wages.<p>
This victory is huge, especially given that the pay increase comes mid-contract. For 18 months, UC insisted that it didn't have the money. But workers picketed and pressured politicians and celebrities to boycott the university, and large numbers of supporters, students and workers got arrested on three separate occasions.<p>
UC buckled to the pressure and "found" the $8.9 million that was there all along for this settlement.<p>
The pay increase is a significant step toward a living wage--so that, as one custodian put it, "Now working two jobs is more of a choice. Before, I didn't have a choice."<p>
This struggle also has developed a layer of strong fighters and rank-and-file leaders who understand that this victory was not given to them, but that they had to fight for every penny that they won. The union starts negotiating a new contract in August. We intend to win that fight, too.<p>
