<p><a name="Madison"></a><font face="Times New Roman, Times, serif" size="4"><b>Madison, Wis., painters</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Benjamin Ratliffe</b></font><p>

<font face="Times New Roman, Times, serif" size="3">MADISON, Wis.--Workers from the International Union of Painters and Allied Trades Local 802 went on strike at midnight June 1 to fight for a living wage.<p>
Negotiations have been ongoing for the last five weeks. Initial efforts by contractors to shorten break times and keep painters from using their own tools were defeated. But when it came to the economic package, the bosses presented workers with what union field representative Jeff Mehrhoff called "a slap in the face."<p>
Workers have been offered a raise of just 69 cents a year for the next three years. Meanwhile, under the current offer, workers would reportedly be forced to pay another 92 cents an hour for health care coverage.<p>
Workers returned shortly after the first negotiations with a proposed interim agreement that was rejected by a group of contractors. Upon rejection of the proposal, Local 802 agreed to strike by a vote of 96 to 10 at the union's largest meeting in recent memory.<p>
According to union members Matthew Nadzieja and Kent Zenefski, who walked the picket line outside the University of Wisconsin at Madison Engineering Building, concessions the bosses are asking for will further diminish workers' paychecks, which already don't provide enough to raise a family. "All we're asking for is a quarter--I say, we should be demanding a dollar," said Nadzieja.<p>
Workers are facing an uphill battle. Some of the contractors have agreed to the interim agreement, which Mehrhoff sees as a blatant attempt to break the union. Workers are also facing scab laborers, some of whom were brought in by the contractors, while others have been organized by a local carpenters' union that split from the AFL-CIO and has a long history of raiding drywall workers from Local 802.<p>
Local supporters are planning help walk picket lines and get the word out about the strike.<p>

