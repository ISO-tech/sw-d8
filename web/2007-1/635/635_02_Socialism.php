<font face="Arial, Helvetica, sans-serif" size="2"><b>EDITORIAL</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why you should join us at Socialism 2007</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 8, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE DAYS when George Bush could boast that one-third of the people living in the U.S. approved of the job he's doing are long gone. His administration's right-wing agenda--especially the centerpiece, the U.S. war on Iraq--has been unmasked and discredited on virtually every point.<p>
But in spite of the widespread opposition to Bush, people committed to the struggle for justice face questions in turning this discontent into a political challenge that can win real change.<p>
What's the way forward for the struggles of today--against the war, for immigrant rights, against the injustices of the justice system, for union rights and better living standards? That question will run through each one of the four days of meetings and discussions at <a href="http://www.socialismconference.org" target="_blank">Socialism 2007</a>, on June 14-17 in Chicago.<p>
Activists from different movements will come together to share their experiences and strategize, and to discuss the latest political developments, whether in Bush's wars abroad or the offensive against working people at home. But equally important will be the meetings on the history and traditions of the socialist movement and the revolutionary ideas of Marxism--which offer an opportunity to deepen our understanding of the world.<p>
<i>Socialist Worker </i>is a proud cosponsor of the annual Socialism conference, and we want to once again extend an invitation to all our readers to join us in Chicago. Political discussion and debate have been as important as protest in the long history of the struggle for a better world, and we hope Socialism 2007 will play a part in bringing together and strengthening the left alternative in the U.S.<p>

