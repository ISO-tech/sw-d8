<font face="Times New Roman, Times, serif" size="4">Libby trial offers look at White House dirty tricks</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Cheney on the hot seat?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 26, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>LANCE SELFA explains what to look for in a trial that could expose the Bush administration's dirty tricks.</b><p>

OPENING ARGUMENTS were likely to begin in the perjury trial of Dick Cheney's former chief of staff, I. Lewis "Scooter" Libby.<p>
Special Prosecutor Patrick Fitzgerald won a grand jury indictment of Libby for perjury over his testimony about the source of a 2003 leak to the press that revealed the identity of CIA undercover agent Valerie Plame, the wife of former U.S. Ambassador Joseph Wilson.<p>
If reports about the prosecution's and defense's questions to potential jurors drawn from the Washington, D.C., area are any indication, the White House might sustain some serious collateral damage from Libby's trial.<p>
Libby lawyers twice asked potential jurors "how they would view Vice President Cheney if his testimony is 'contradicted' by another witness," wrote MSNBC's David Shuster. Fitzgerald's deputies, meanwhile, asked jurors if their opinion of the vice presidency would be lowered if Cheney was aggressively questioned.<p>
In other words, Cheney looks like he will be on the hot seat as much as Libby.<p>
Fitzgerald's investigation and Libby's trial stem from a crude White House attempt to silence an early critic of the Iraq war.<p>
In 2002, Wilson, an experienced diplomat, undertook a mission at the CIA's request to investigate allegations peddled by Iraqi expatriates that the Saddam Hussein regime had procured nuclear materials from Niger as part of a covert weapons program. Wilson's 2002 report debunked those rumors.<p>
Nevertheless, the administration and its water-carriers in the media continued to peddle the falsehood in their propaganda for war against Iraq--leading Wilson to reveal his findings publicly in a July 2003 <i>New York Times </i>article.<p>
Because the article exposed the administration to charges of lying and manipulation of intelligence, the White House launched a crude attempt to discredit Wilson by "outing" his wife--to suggest that Wilson had taken a junket to Africa at his wife's request.<p>
Libby and Bush's top political adviser, Karl Rove, were involved in the campaign to smear Wilson to the Washington press corps. Fitzgerald is investigating whether outing Plame constitutes a violation of federal law that forbids revealing covert operatives' identities.<p>
At its root, the Plame case is about the single biggest issue bringing down Bush and the Republican Party--the war in Iraq.<p>
In 2003, the administration and the Republicans thought a quick victory in Iraq would make them unassailable in 2004--as well as delivering an example to all U.S. adversaries about the "Bush doctrine" of pre-emptive war and regime change. But within a few weeks, it became clear that the rosy scenario wasn't working out.<p>
And when Wilson went public with his revelations, it threatened to unravel the entire administration case for war, based as it was on deceit and manipulation of public fear. Inside the White House, Cheney and Rove were reportedly obsessed with discrediting Wilson--because they felt his revelations, along with the U.S. failure to find "weapons of mass destruction" in Iraq, could cost Bush the 2004 election.<p>
Today, the Libby trial and the Plame scandal may seem anti-climactic after the Republican drubbing at the polls last November. But it still has the potential to take a toll on the White House--whether Libby is convicted or not.<p>

