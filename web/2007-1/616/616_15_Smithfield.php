<font face="Times New Roman, Times, serif" size="4">Hundreds take a day off the bosses denied</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Smithfield workers protest on MLK day</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 26, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">TAR HEEL, N.C.--Some 500 workers at the Smithfield Foods pork-processing plant here stayed off the job or walked out January 15 in recognition of Martin Luther King Day.<p>
The action was especially significant given that the plant's 5,500 workers have been waging a fight for dignity on the job--displayed spectacularly in November when some 1,000 workers walked off the job. The two-day wildcat strike in November was sparked when the company fired dozens of immigrant workers for supposedly not having proper Social Security documentation.<p>
Smithfield, the country's largest and most profitable pork processor, is known for subjecting its workforce to punishing hours, dangerous working conditions and anti-union harassment.<p>
Management has a long history of attempting to whip up racism between Latino and Black workers, who both make up a large percentage of the workforce. That's one of the reasons, workers and activists say, the King Day action is so inspiring.<p>
Currently, Smithfield workers are allowed eight holidays off per year. When they recently asked for the right to observe Martin Luther King Day, management flatly refused.<p>
Company spokesperson Dennis Pittman told reporters that workers had a chance to have the day off few years ago--but voted to take Easter off instead. This, despite the fact that Martin Luther King Day is recognized in the state of North Carolina.<p>
Thousands of workers signed a petition in support of the right to observe the holiday as a paid day off. But when management refused to even look at the petition and announced that workers would receive demerit points, lose a day's pay and could be fired for not showing up at the plant, civil rights leaders and community members joined Smithfield workers in their call for justice.<p>
In the run-up to the holiday, a group of clergy published an open letter in the <i>Fayetteville Observer</i> asking the company to "heal the wounds of injustice against the workers and recognize the holiday," and the Rev. Jesse Jackson spoke out in defense of the workers. Supporters sent 6,000 e-mails and made hundreds of phone calls to the company.<p>
When the day came, hundreds of workers on the first shift either stayed away or walked off the line. Though Smithfield management claimed that there were no more absences than the usual 100 to 150, the Smithfield Justice campaign said that as many as 500 were absent--forcing the plant to switch production for a time to a single line, a significant slowdown.<p>
As a result of the walk-off, and the public embarrassment, Smithfield is now discussing allowing workers to take the day off in the future. But the fight for justice at Smithfield is far from over.<p>
<i>For more information or to send an e-mail to Smithfield management in support of the workers, visit <a href="http://www.smithfieldjustice.com" target="_blank">www.smithfieldjustice.com</a></i>.<p>

