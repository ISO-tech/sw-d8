<p><a name="Housing"></a><font face="Times New Roman, Times, serif" size="4"><b>Affordable housing is a right</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Hannah Wolfe</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--"They want us out, and they will do <i>anything</i>," said tenant Rita Hawkins at a standing-room-only meeting of more than 200 Harlem tenants in the basement of Abyssinian Baptist Church on January 10. The meeting was organized by the Buyers and Renters United to Save Harlem, Goddard Riverside's West Side SRO Law Project, and others.<p>
Lightning-paced gentrification--with real-estate values more than quadrupling over the past decade--has meant an increase in Harlem landlords' harassment of low-rent tenants.<p>
"Each year, 10,000 apartments lose their affordability when they are removed from Rent Stabilization or Rent Control because of 'high-vacancy decontrol,'" said meeting organizers. "[M]any unscrupulous landlords, such as the notorious Pinnacle Group...buy buildings with the expectation that they will be able to maximize profit by using harassment tactics to push out existing residents."<p>
Meeting organizers are pressing for a Tenant Harassment Bill, which would make a pattern of harassment by a landlord illegal. Because there is no explicit law against landlord harassment, tenants are forced to take landlords to court for each incident of harassment. This requires considerable time and resources, which most tenants don't have.<p>
Handouts enumerating the 14 most common forms of harassment--ranging from denial of services to physical threats and sexual harassment--were distributed at the meeting. Meeting attendees noted an increase in aggressive sexual harassment of single mothers and intimidation based on tenants' immigration status.<p>
Tenant Carole Singleton called for solidarity. "You don't have to like your neighbor, but you have to stick with them. You must make a stand," Singleton said. "All of us have been one paycheck away from the street. If it's not you now, you will be next. You all have the power and you must use it. We can only do this as a collective."<p>
A "Convention to Select New York's Most Abusive Landlord" is being organized later in January at the Judson Memorial Church.<p>
