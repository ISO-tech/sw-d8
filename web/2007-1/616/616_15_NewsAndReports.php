<p><a name="MLK"></a><font face="Times New Roman, Times, serif" size="4"><b>Celebrating Martin Luther King's legacy</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IN SEVERAL cities across the U.S., marchers turned out January 15 to honor the legacy of Rev. Martin Luther King, Jr.<p>
--In <b>Seattle, </b>more than 2,000 people rallied under the theme of "Peace, Justice and Human Rights," and marched through Seattle's Central Area to the Martin Luther King Jr. Park. Before the rally, participants attended workshops on topics including immigrant rights, peace and justice and health care.<p>
Lt. Ehren Watada, who is currently facing court martial for refusing to fight in Iraq, spoke to the crowd. Hundreds of signs in support of Watada and other resisters were on display, and strong contingents included those organized by Iraq Veterans Against the War and Veterans for Peace. Unions and immigrant rights supporters were also prominent.<p>
--In <b>Atlanta, </b>a parade and rally drew as many as 10,000. Although the numbers at this year's event were smaller than in previous years, a sizable antiwar crowd was on display.<p>
Earlier in the day, at the annual program at the Ebenezer Baptist Church, pastor Rev. Raphael Warnock called out the Bush administration, saying, "We need a surge in consciousness and truth-telling for a change."<p>
--In <b>San Diego, </b>as many as 75 people attended an alternative to the city's heavily military-oriented parade. Organized by the King/Chavez Coalition, the alternative event featured speakers and a potluck, and was held in a predominantly minority community.<p>
"I feel privileged to have been a part of such a monumental 'first annual' and cannot wait to see how it grows for next year," Gloria Mosi, one of the organizers, told <i>Socialist Worker</i>.<p>
<i>Ty Coronado, Steve Leigh and Jonathan Wexler contributed to this report</i>.<p>

