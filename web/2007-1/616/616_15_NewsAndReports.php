<a name="Abortion"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend abortion rights</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Corrie Westing</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--For the third year, San Franciscans gathered to protest anti-choice forces in the "March for Life" on January 20 for the anniversary of <i>Roe v. Wade, </i>the 1973 Supreme Court decision that made abortion legal.<p>
About 200 abortion rights supporters came to protest some 20,000 anti-choice marchers bused in from around the country and to show that we won't give up the fight to defend women's right to choose.<p>
The police shoved pro-choice protesters to the back of the sidewalk, blocking them in with no way to exit or cross the street for blocks--claiming that the anti-choice protesters needed to march on the sidewalk, since they didn't have a permit to march on the street. Then the anti-abortion protesters marched on the sidewalk <i>and </i>the street, as pro-choicers shouted them down from the sidelines.<p>
In the context of the escalated attacks on women's right to control her reproductive life, the counterprotest, though small, was an important show of defiance. The march has occurred in Washington, D.C., every year since abortion was legalized, but the last few years the marchers have come to pro-choice San Francisco, representing a new offensive against women's rights.<p>
The first year the March for Life announced it was coming here, many liberal, progressive and revolutionary groups successfully organized against it. In the last two years, however, mainstream women's groups that could have turned out big numbers to oppose the bigots have refused to participate--leaving only smaller forces, with fewer resources, to put together a counterprotest.<p>
This has lead to some understandable disappointment and disgust among progressives, who can't stand to see such a reactionary bunch march virtually unopposed.<p>
Among the signs that anti-choice marchers carried were "Abortion Hurts Women" and "Women Deserve Better Than Abortion." Pro-choice protesters were quick to point out the hypocrisy.<p>
"Does anyone else want to vomit?" Alison, a young woman with a hand-made pro-choice shirt and sign, asked. "Women deserve better? How about women deserve a right to control their own bodies? How about women deserve not to die in the tens of thousands from illegal back-alley abortions? That's the message I hope we send today."<p>
