<font face="Times New Roman, Times, serif" size="5"><b>Iraq war vets speak out in Burlington</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jessica Zamiara</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 26, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">BURLINGTON, Vt.--Approximately 130 people packed the City Hall auditorium January 19 for "Bring the Troops Home Now!" a panel discussion featuring Iraq Veterans Against the War members Drew Cameron and Matt Howard; Burlington Mayor and Progressive Party member Bob Kiss; state Sen. Ginny Lyons; and International Socialist Organization member Helen Scott.<p>
Cameron and Howard both received standing ovations after telling their stories of the U.S. military's insufficient support of troops at war and at home, and of their personal transformations into antiwar activists.<p>
Howard, a two-tour veteran of Iraq, said his time spent in the Middle East was "chaotic from the first second." Speaking of the escalating Iraqi resistance, he commented, "Our presence will always inflame insurgency. The people are fighting for their country."<p>
Helen Scott spoke of the connections between U.S. imperialist aims and oil corporation greed in the Middle East, pointing out the hypocritical history of the United States' involvement in Iraq.<p>
Sen. Lyons, meanwhile, talked about the resolutions in support of immediate withdrawal that she will be submitting to Congress. "We had a choice when we went into Iraq, now we have a choice of where we want to go--back home!" she said.<p>
Drew Cameron summed up the sentiment in the room when he stated, "Military occupation is not, will not, and can never, benefit the people of Iraq."<p>
To hear these words come from a veteran upped the morale in the room tenfold. That morale continued through to the next day where, in Montpellier, Vt., approximately 350 people marched from the City Hall to the Vermont state house.<p>
Speakers at the rally included Cameron and Howard, as well as Marine Sgt. Liam Madden, who celebrated his last day as an active duty soldier by calling for his brothers and sisters to come home now and bring an end to this brutal occupation.<p>
With the January 27 antiwar demonstrations approaching, Vermont antiwar activists are working hard to bring an end to this criminal war. As Matt Howard stated, "We can't win a crime, we can only stop it."<p>

