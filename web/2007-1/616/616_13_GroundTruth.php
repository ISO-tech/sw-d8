<font face="Times New Roman, Times, serif" size="5"><b>Veterans expose the recruiters' lies</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Jon Van Camp</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 26, 2007 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i><a href="http://www.thegroundtruth.net" target="_blank">The Ground Truth</a>,</b></i> a documentary by Patricia Foulkrod.</font><p>

<font face="Times New Roman, Times, serif" size="3">AS A high school teacher in a working-class school, I come across students all the time who are being courted by military recruiters. Often these students are personally against the war in Iraq and despise the Bush administration, but they fail to see the connection between right-wing policies and "the toughest job they'll ever love."<p>
An antidote to this attitude comes in the form of a powerful new documentary out on DVD, <i><a href="http://www.thegroundtruth.net" target="_blank">The Ground Truth</a></i> by Hollywood director and peace activist Patricia Foulkrod. The film focuses exclusively on Iraq war veterans--many of whom are now prominent antiwar activists such as Aidan Delgado, Jimmy Massey and Camilo Mej&iacute;a--and allows them to describe the brutal reality of the war and their disillusionment with the military.<p>
We are lead through the various steps soldiers go through from recruitment to combat in Iraq to the grueling aftermath of war. Veterans first recount how they were misled and lied to by recruiters when they joined.<p>
One soldier was told they were not sending any more troops to Iraq so there was no need to worry. Others were enticed with promises of college benefits and training. As one veteran pointed out, however, no recruiter talked about your primary purpose in the military--which is to kill.<p>
One of the most shocking parts of the film is the description of basic training. Soldiers are consistently desensitized to death and killing. Realistic video games allow soldiers to simulate battle while killing cartoon characters.<p>
The cadences that the new recruits sing as they march are chilling: "Ring the bell inside the schoolhouse. See those kiddies gather round. Lock and load with your 240. Mow those little motherf----rs down."<p>
Even those soldiers who were shocked when they heard this found themselves singing along to lyrics like these. When they get to Iraq, the soldiers encounter killing for real.<p>
Incredible footage depicts the ease with which human beings are mowed down from a distance. One scene shows the view from what looks like a mechanical drone, which drops a bomb and annihilates at least 50 people instantaneously.<p>
One veteran describes his torment at having killed an innocent woman who was carrying a white flag. Another describes how a soldier slows down a vehicle to let a child cross the road, then speeds it up to run the child over. The incident is shrugged off because military regulations prohibit vehicles from slowing down or swerving to avoid civilians.<p>
After the soldiers return, they find that far from being a benefit, their military tour has destroyed a part of them. Soldiers talk about the limbs they lost, and the physical scars and mutilation. But even worse are the emotional scars.<p>
One veteran describes getting in a fight at a bar and instinctively pulling out his gun and pulling the trigger. Only a malfunctioning weapon prevented a tragedy and a long prison term.<p>
Jimmy Massey describes how he went to the Veterans Administration hospital for help with post-traumatic stress disorder. When he tells the psychologist he has trouble dealing with all the innocent people he saw killed, the psychologist tells him he will be unable to help him because they do not deal with conscientious objectors.<p>
Many of the veterans in the film are members of Iraq Veterans Against the War, and the film describes their journey from disillusioned veterans to activists. Camilo Mej&iacute;a goes to prison because he refused to return to the war in Iraq. Others are shown participating in protests and speaking out around the country.<p>
As one veteran said, these veterans still care about duty, but their real duty was not in Iraq--it is fighting against the war here at home. Ultimately, the beauty of <i>The Ground Truth</i> may be its impact on those who watch it.<p>
I showed this film to my 11th grade World History classes before winter break. After break, we had a discussion about it, and at least half a dozen kids told me they had been seriously considering the military, and at least one was ready to enlist, but after seeing this film they said they would never join.<p>
Hopefully, this film and others like it can continue to have an impact like this.<p>

