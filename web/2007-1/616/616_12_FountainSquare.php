<font face="Times New Roman, Times, serif" size="5"><b>Fountain Square's corporate takeover</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 26, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">AFTER BEING closed to the public for a year for redevelopment, Cincinnati's Fountain Square was finally reopened on October 14, 2006. Fountain Square is one of the most familiar landmarks in the city. Most Cincinnatians will tell you they have been there for a rally, a celebration or a protest; or for a political, social, religious or sports event.<p>
Until it was taken over in 2003 by 3CDC, Fountain Square was one of the few remaining commons in downtown Cincinnati.<p>
Before 3CDC became a corporation, it was an advisory board to the Research and Development Department for the city of Cincinnati, originally formed to advise the department on how best to use various federal funds, including "Empowerment Zone" funds--a federally funded program originally intended to help lift several designated neighborhoods out of poverty.<p>
In July 2003, this board was officially incorporated as a nonprofit organization and took the name the "Cincinnati Center City Development Corporation," or "3CDC." The city council then voted to turn over the task of redeveloping Fountain Square, the banks of the Ohio River and Washington Park--which is located in Over-the-Rhine, one of the blighted areas mentioned above--to 3CDC. After that, the city council voted to close the city's Research and Development Department.<p>
The following information was taken directly from the 3CDC Web site: "The City of Cincinnati has committed to invest $100 million over five years to support Center City plan projects. This amount is consistent with its historical annual capital investment in City development projects.<p>
"Funds will primarily come from Downtown and Over the Rhine Tax increment Financing (TIF). In addition, Over-the-Rhine is eligible for Empowerment Zone funds and Federal Community Development Block Grants.<p>
"The private sector will create the Center City Development Fund with the goal of raising $50 million in private sector capital to help fund Center City Projects. 3CDC will also work closely with the Cincinnati Development Fund, a community development financial institution focused principally on housing."<p>
When it was incorporated, most of the members of the 24-member board of directors were CEOs or heads of some of Cincinnati's largest corporations or institutions.<p>
As of this writing, 3CDC has bought up several blocks of property in Over-the-Rhine, much of which was low-income housing for the poor, and many of the people living there were forced to move so that more market-rate housing could be made available.<p>
Personally, I don't think that gentrification of the blighted neighborhoods was the original intent of Empowerment Zone funding.<p>
On December 20, 2006, the city council approved a budget for the next two years which took millions of dollars from services and programs for the poor people of Cincinnati in order to balance the budget.<p>
Funding was cut from several neighborhood health clinics, and some were closed down altogether. The Free Store, which provides food, clothing and various other services for Cincinnati's poor, lost funding. Homeless shelters and senior services lost funding, and seven neighborhood swimming pools are to be closed.<p>
3CDC, however will continue to receive its millions of dollars from the city of Cincinnati, and 60 more police officers will be employed to protect their interests.<p>
As for Fountain Square, on December 16, a group of International Socialist Organization members were downtown selling <i>Socialist Worker</i> when two of us wandered up onto the square. We were met by a gentleman who informed us that Fountain Square was now "private" property, and we could no longer sell anything there without a special permit. Fountain Square has indeed been "privatized."<br>
<b>Vera Zlatkin</b>, Cincinnati<p>

