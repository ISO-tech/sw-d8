<font face="Arial, Helvetica, sans-serif" size="2"><b>VIEWS AND VOICES</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Using Frederick Douglass' legacy for political gain</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>O'Malley isn't on our side</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 26, 2007 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">IN THE long and ignominious tradition of politicians twisting the words of dead radicals to serve their own ends, former Baltimore Mayor and Maryland's new governor, Martin O'Malley solemnly intoned the words of "Marylander" Frederick Douglass after being sworn at the January 17 inaugural in Annapolis: "Remember that we are one, that our cause is one, and that we must help each other, if we would succeed."<p>
I guess it's a more inspiring way of saying "BELIEVE"--the Orwellian directive printed on huge vinyl banners across the crumbling facades of Baltimore's public schools and printed on the boxes housing the police video cameras fixed above our city's streets.<p>
Douglass believed in, and was part of, the struggle of the oppressed for dignity and freedom against brutal exploitation. He was a "native Marylander" only because he was born into chattel slavery on the eastern shore, before he escaped to find freedom in the North, where he became a leader in the abolitionist movement.<p>
Douglass, along with co-editor Martin Delany, wrote the words so misused by O'Malley in his radical abolitionist journal, <i>The North Star</i>, printed from a tenuous pre-Civil War freedom and addressed to his brothers and sisters still in bondage: "Remember that we are one, that our cause is one, and that we must help each other, if we would succeed.<p>
"We have drank to the dregs the bitter cup of slavery; we have worn the heavy yoke; we have sighed beneath our bonds, and writhed beneath the bloody lash; cruel mementoes of our oneness are indelibly marked on our living flesh.<p>
"We are one with you under the ban of prejudice and proscription--one with you under the slander of inferiority--one with you in social and political disfranchisement. What you suffer, we suffer; what you endure, we endure. We are indissolubly united, and must fall or flourish together."<p>
Thus, the "we" who "are one" was not meant to include white politicians who preside over the legal lynching of Black men sitting on one of the country's most racially disproportionate death rows. Or mayors who brag about the effectiveness of their city's police force, while officers of that force are charged with forcing women to have sex with them in exchange for their freedom, and whose corrections officers are convicted of stomping a prisoner to death in the city jail.<p>
Governor O'Malley, there is indeed a "we" whose "cause is one," and who "must help each other, if we would succeed." We must succeed in getting the court-ordered $1 billion owed to Baltimore's schools from the state treasury. We must succeed in getting your crooked cops out of our neighborhoods. We must succeed in abolishing Maryland's racist death penalty.<p>
You can decide to help, but until you do, your attempt to turn the call to arms of Frederick Douglass into an empty platitude for "One Maryland" makes you nothing more than a new overseer.<br>
<b>Ben Dalbey, </b>Baltimore<p>

