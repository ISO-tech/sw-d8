<a name="Transit"></a><font face="Times New Roman, Times, serif" size="4"><b>New York City transit workers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Peter Lamphere</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--State arbitrators have finally imposed the contract that New York City transit workers struck over and then voted down.<p>
Members of the 30,000-strong Transport Workers Union (TWU) Local 100 no doubt greeted the deal with relief, as many feared the board of arbitrators would impose a worse contract than had been negotiated at the end of the strike.<p>
Instead, the major concessions agreed to by both the Metropolitan Transportation Agency (MTA) and the union remained in place: Members will be paid reimbursements owed to them for higher pension contributions in the 1990s, but will be forced to pay 1.5 percent of gross wages for retiree health care benefits.<p>
Anger about the health care concession, unprecedented in the history of city unions, spilled over into the TWU Local 100 election.<p>
Incumbent President Roger Toussaint held onto his post, but without even winning a majority of votes. Toussaint's allies lost a majority of the vice president positions elected by division, and his support on the executive board fell by seven seats.<p>
Discontent with Toussaint ran particularly high among members in the private lines that are in the process of being incorporated into the MTA, and in the Rapid Transit Operations division of the union, which encompasses conductors and train operators.<p>
Toussaint was first elected president in 2000 against the local's old guard. His electoral vehicle, New Directions, was the product of years of rank-and-file organizing. But Toussaint turned on his supporters, leaving rank-and-file forces in disarray that remained evident in the election results.<p>
Many who had supported New Directions decided to ally themselves with members of the deposed old guard behind the Rail and Bus United slate, which won about 35 percent of the vote in the presidential election.<p>
Other former New Directions activists formed an independent team of candidates, but opted not to challenge Toussaint in the election for president and were defeated by wide margins--partly by undemocratic ballot rules--in several attempts to win a vice president seat.<p>
In one bright spot, Steve Downs, an original New Directions activist, won the chairperson position in the historically militant train conductors division.<p>
Toussaint will no doubt have difficulty maintaining his autocratic style with his clearly reduced mandate. Plus, the arbitration decision, while it does impose the giveback deal on which Toussaint pushed through a re-vote, also vindicates those who voted against it in the face of Toussaint's dire warnings that arbitration would be a disaster.<p>
The future of the union depends on rebuilding rank-and-file organization, which Toussaint took over as his own personal vehicle. The election results may have opened up space to begin that task.<p>

