<p><a name="Borat"></a><font face="Times New Roman, Times, serif" size="4"><b><i>Borat</i> doesn't help our side</b></font><p>

IN THEIR views on the movie <i>Borat</i>, both Brian Jones ("Comin' to America," November 17) and Brian Lenzo ("Too hard on <i>SW</i>'s review," December 15) end up trivializing the bigotry that permeates the movie and the world.<p>
While the movie has funny moments where it shows and exposes the deep prejudice embedded in American society (for example, how crazy right-wing evangelicals are), the problem is in how the movie does so.<p>
In a racist society like ours, making fun of racism (or sexism and homophobia) by being racist does more to perpetuate racism than undermine it. Not only does the movie perpetuate anti-Muslim prejudice (Crazy Muslims don't let women drive! Let's liberate them!), but the whole portrayal of Kazakhstan, the people and culture, perpetuates racist ideas about the "third world" in general: that people in "third world" countries are backwards, dumb and poor (They can't run their own countries--let's do it for them!).<p>
Average Americans know nothing about Kazakhstan. Most of my college students can't even locate it on a map. It sickens me that, for many people, this movie is their first introduction.<p>
So while progressives can find instances of humor in <i>Borat</i> and a TV show like <i>South Park</i>, let's be clear that other people are laughing for different reasons than ours; because they are racist, or think it is okay to be racist (<i>South Park</i> is written by a couple of libertarians, by the way). This is not what "moving consciousness forward" looks like.<p>
While the title is unfortunate, if progressives want a good laugh, you should watch Roseanne Barr's <i>Blonde and Bitchin'</i>, available on DVD. <br>
<b>Michael Estrada</b>, San Francisco<p>
