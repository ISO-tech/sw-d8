<a name="Pinochet"></a><font face="Times New Roman, Times, serif" size="4"><b>Pinochet's accomplices</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHILEANS HAD plenty to celebrate this New Year's. Just weeks before 2006 came to an end, the news that hated dictator Augusto Pinochet had died brought joy to millions in Chile and around the world.<p>
On September 11, 1973, Pinochet came to power through a violent military coup supported by the U.S. During his 17-year rule, Pinochet turned the Andean nation into a laboratory for state terror and the neoliberal politics that would drive millions into poverty throughout the region in subsequent decades.<p>
Amid fireworks, champagne and shouts of "&iexcl;&Eacute;l que no salta es Pinochet!" (If you don't jump you're Pinochet!), I had the good fortune of ringing in the New Year in Chile with a Chilean friend of mine who grew up in exile in Norway. Her parents won a "Pinochet scholarship" to that country after having occupied the Venezuelan embassy with other students in 1977 demanding refugee status.<p>
Her father, a teenager and student leader, had been among the thousands rounded up and tortured in the national stadium in the wake of the coup that brought Pinochet to power in 1973. He wasn't murdered, as an estimated 3,000 were, but survived underground until he hopped the embassy fence. His family was harassed and his brother detained and tortured to find his whereabouts.<p>
The story of my friend's family is not an unusual one--thousands of families were torn apart by the violence of dictatorship. These families' joy at the dictator's death is tempered by a sense of justice denied, echoed by the cover of one Chilean magazine, <i>The Clinic</i>, that ran the headline "El perro muerto--se fue sin pagar" (The dead dog--he left without paying), alongside a photo of the bloated face of the former dictator Pinochet's corpse, with a superimposed Hitler moustache.<p>
Pinochet narrowly escaped trial for his crimes, but his accomplices are still alive in Chile and abroad. Former U.S. Secretary of State Henry Kissinger not only had a role in the coup, but helped Pinochet export his model of repressive sadism to neighboring Argentina, Bolvia, Paraguay and Brazil through "Plan Condor" that coordinated information among those countries' secret police forces.<p>
It's time Kissinger and his ilk were held responsible.<br>
<b>Karl Swinehart</b>, Los Angeles<p>
