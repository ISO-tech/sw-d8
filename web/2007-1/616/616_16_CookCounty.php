<font face="Times New Roman, Times, serif" size="3">The cuts would also eliminate workers--as many as 6,200 employees could be fired.<p>
On January 18, some 300 registered nurses (RNs), who are represented by National Nurses Organizing Committee/California Nurses Association (NNOC/CNA), picketed in front of Stroger Hospital, the former Cook County Hospital and one of the largest public hospitals in the U.S. Chanting "Stop the cuts!" and carrying signs reading "Don't balance the budget on our patients' backs!" the union vowed to keep up the pressure on the county board.<p>
"Nobody wins when patients are shortchanged," said Sheilah Garland-Olaniran, NNOC/CNA Midwest collective bargaining coordinator. "The county commissioner's plans to cut 17 percent across the board would be a health care disaster for all of us. We would likely see an increased number of deaths, more stress on the county's already overburdened emergency rooms, and area hospitals straining to absorb county's patients."<p>
Doctors are also facing layoffs. Currently, there are 500 doctors at Stroger, 100 at Provident, and 86 at Cook County Jail or in community-based clinics. The threat to their jobs led 200 doctors to file a petition with the Illinois State Labor Board seeking to unionize with Service Employees International Union Local 20.<p>
The county hospital and clinic system is so overwhelmed that 11,000 women are on a waiting list for mammograms. The wait time for a routine colonoscopy is six months. County community health centers are overbooked and can't schedule new patient appointments for two or more months.<p>
A study done last year in the county emergency room found that wait times were so long that many patients simply don't return. "We're busting at the seams already," Martese Chism, a nurse at Stroger and an elected union official told the <i>Chicago Tribune</i>. The county's cuts are "going to push us over the edge."<p>
Some of the cuts have already gone through. For example, administrators this month slashed weekly hours for a walk-in clinic near Stroger Hospital that provides services for over 100,000 patients a year.<p>
The clinic began turning people away who had come for care at 2 p.m. Two days later, the situation was even worse, and the clinic couldn't accept any more patients after noon because of long lines. The hospital's ER was then flooded with patients. "We're only three days into this, and it's already a mess," a doctor told the <i>Tribune</i>.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">THE SKYROCKETING number of people who are uninsured is the reason that emergency rooms are overloaded. The metropolitan region of Chicago has 1 million uninsured, and the county system serves as the medical safety net for the entire region.<p>
In a classic attempt to pit patients against one another, County Board President Stroger sent out a memo stating, "All patients who present to any of our facilities or clinics must show proof that they are residents of Cook County during the registration process. Those who are not residents of Cook County must be referred to the Public Health Department of their respective counties, or they must pay full price." Stroger only rescinded the memo after the <i>Chicago Tribune </i>ran a story about it.<p>
Dr. David Goldberg, a physician at Stroger Hospital wrote a moving article about the planned budget cuts and their impact.<p>
"I am the son of Holocaust survivors, and I grew up in the civil rights era," Goldberg wrote. "My desire for a just and human society is deeply ingrained. To me, quality medical care is a basic human right. When I came to Cook County Hospital as a medical student 25 years ago and then stayed on as a resident physician, I discovered my calling: combining medicine and social justice."<p>
It is in that spirit that we need to build a movement against the cuts--and to make medical care a human right.<p>

