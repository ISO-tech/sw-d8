<font face="Times New Roman, Times, serif" size="4">Cook County health care workers say no</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Public health care cut to the bone</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Lalasz and Helen Redmond</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 26, 2007 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--Cook County politicians are proposing drastic cuts to one of the nation's largest public health care systems. But nurses and doctors aren't taking these cuts lying down.<p>
Cook County Board President Todd Stroger announced a 17-percent cut in medical services to eliminate a budget deficit of more than $140 million. The cuts could destroy an already overburdened and underfunded system that serves the poor and uninsured from throughout the Chicago area.<p>
On the chopping block are a dozen community-based primary care clinics; labor and delivery, pediatric and nursery services at Provident Hospital; the departments of occupational medicine and plastic surgery at the county's main public facility, John Stroger Hospital; and dental, health and substance abuse services in the county prison system.<p>
"If you close clinics or portions of certain hospitals, where are people going to get care?" asked Jim Pandazides, a critical care nurse.<p>

