<font face="Times New Roman, Times, serif" size="3">Defending war resisters is a critical part of ending the war, because it gives confidence to other soldiers considering their options as Bush plans a "surge" of 21,500 more troops to Iraq.<p>
During the U.S. war on Vietnam, the revolt of rank-and-file soldiers--who refused combat missions and resisted deployment--hampered the effectiveness of the military as a fighting force and ultimately was critical to ending that war, along with the resistance of the Vietnamese themselves and the mass antiwar movement in the U.S.<p>
Today, the seeds of a GI movement opposed to the war in Iraq are taking root. On January 16, Seaman Jonathan Hutto, Marine Sgt. Liam Madden and Army National Guard Sgt. Jabbar Magruder delivered their Appeal for Redress (online at appealforredress.org), signed by 1,034 active-duty troops, to Congress.<p>
Hutto was inspired to organize active-duty opposition to the war after reading David Cortright's book <i>Soldiers in Revolt</i>, which chronicles the GI rebellion during the Vietnam War.<p>
Opinion polls show that large numbers of troops are unhappy about their situation. Just one in five agree with Bush that they should stay in Iraq "as long as they are needed," according to a Zogby poll released almost a year ago. Seventy-two percent thought the U.S. should withdraw by the end of the year.<p>
With two-thirds of the public opposed to Bush's surge option, it's certain the opposition is even higher today.<p>
But for soldiers to act on this discontent in ways that hinder the war effort, the antiwar movement needs to make its presence felt.<p>
At a time when Democrats and Republicans talk about "supporting the troops," but won't bring them home, the January 27 protests--in Washington, D.C., and other cities around the U.S.--will play a critical role. They can show U.S. soldiers that they have a powerful ally in the antiwar movement--and give confidence to those who choose to fight back by resisting the U.S. war machine.<p>

