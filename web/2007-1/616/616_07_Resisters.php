<font face="Times New Roman, Times, serif" size="5"><b>The new GI resisters</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 26, 2007 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">SOME 350 people attended a hearing in Tacoma, Wash., on January 20-21 to put the U.S. war--not war resisters--on trial.<p>
They heard testimony that the U.S. war on Iraq is illegal and immoral from Iraq war veterans like Chanan Suarez D&iacute;az and Harvey Tharp; former diplomat Ann Wright; and former Defense Department analyst Daniel Ellsberg, who in 1971 leaked the top-secret Pentagon Papers that exposed U.S. lies about the war in Vietnam.<p>
Also on hand was Army Lt. Ehren Watada, the first officer to publicly condemn the war in Iraq and refuse to deploy. Watada now faces up to six years in a military prison on charges related to his decision to follow his conscience and not go to Iraq last year.<p>
Activists in the Northwest and around the country are planning a February 5 day of action to show support for Watada, timed to coincide with the beginning of the Army's court-martial against him.<p>

