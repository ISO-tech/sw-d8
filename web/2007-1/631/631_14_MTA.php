<font face="Times New Roman, Times, serif" size="5"><b>Treating workers as disposable</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 11, 2007 | Page 14</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>TWU Local 100 member DAN CLEMENTE on two deaths in the New York subway system.</b><p>

NEW YORK--Two Metropolitan Transit Authority track workers died within five days, and now questions are being raised about how safe such workers really are while performing their duties.<p>
A subway train hit Daniel Boggs late on the night of April 24, dragging him to death. On April 29, another train struck and killed Marvin Franklin while he was removing debris, and seriously injured another worker, Jeff Hill.<p>
As a transit employee, this angers me a lot. Since the incidents, Train Service Supervisors (TSS) have distributed memos about the importance of not being complacent. One TSS even had had the gall to tell me that most employees of the MTA are complacent about track safety, thus blaming my coworkers for their own tragic deaths. In addition, 6,000 workers were ordered for "retraining."<p>
Oh, really? How can the burden be on us when we already work in hazardous conditions? The Federal Transit Administration claims these accidents are no surprise. Five track workers nationally have died in the first four months of this year, compared to four in all of 2006. Only two died in 2005. In New York's Metropolitan Transit Authority system alone, seven track workers have died since 2001 and 25 have perished since 1980, not including the latest fatalities.<p>
In most cases, even train operators and conductors, including females who are pregnant, have to walk the structure, whether the track is underground or elevated. If it's underground, we have to breathe in steel dust. If it's elevated, especially in wet conditions, we risk falling down and either falling off and onto the streets or onto a third rail and executing ourselves.<p>
As a conductor, I have to have a two-way radio. If something happens on my train, I can call the Rail Control Center. Why is it that track workers don't have radios?<p>
Track worker Artist McKenzie was right when he told A.M. New York, "This is a billion-dollar corporation with modern technology. We should be able to talk to each other anywhere in the system. If Boggs had had a radio and/or a working emergency phone box at his disposal at the train station, where this happened, they could've alerted him of the last train of the night entering the area.<p>
I also find Franklin's death unnecessary because management ordered him, by himself, to cross the tracks--knowing a train could come at any time--with little equipment. Track workers are under stress to produce quickly without delaying service. Management called it tragic but reckless. The union calls it needless. I call it an inevitable result of MTA speedups.<p>
Despite what most transit riders don't know, when they ride a train or bus, transit workers' jobs are very hazardous. No amount of legislation or bills that are being bandied about right now by politicians in New York City will change it.<p>
We demand better and more modern equipment and safer conditions right now! We are not disposable!<p>

