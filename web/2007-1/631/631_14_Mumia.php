<font face="Times New Roman, Times, serif" size="5"><b>Mumia supporters plan protest</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Wengraf</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 11, 2007 | Page 14</font><p>

<font face="Times New Roman, Times, serif" size="3">PHILADELPHIA--After 25 long years, the case of Mumia Abu-Jamal, a former Black Panther and world-renowned journalist known as the "Voice of the Voiceless" will be heard in federal court. Supporters of the Pennsylvania death row prisoner and activist are planning protests to coincide with his Third Circuit Court date scheduled for May 17.<p>
Mumia was railroaded in 1982 for the murder of Philadelphia police officer Daniel Faulkner. His trial has been denounced by activists and dignitaries all over the world--including Amnesty International, Bishop Desmond Tutu and the European Parliament--as rife with blatant racism and injustice.<p>
Witnesses have come forward to say that they were intimidated into testifying against Mumia; evidence was suppressed; ballistics evidence didn't match the weapon in Mumia's possession; trial judge Albert Sabo displayed outrageous bias, going so far as to say he was going to "help fry the n---." For all these reasons and more, Mumia deserves a new trial.<p>
Finally, Mumia's case will be heard in a federal appeals court on four key issues, including whether Judge Sabo's racist comments violated Mumia's constitutional rights and whether Black jurors were excluded from the jury pool.<p>

