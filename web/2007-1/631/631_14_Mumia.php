<font face="Times New Roman, Times, serif" size="3">At Mumia's trial, the prosecutor used 11 of his 15 peremptory challenges to remove Black jurors. The NAACP and the National Lawyers Guild have submitted amicus briefs in his support. Mumia's lead attorney, Robert Bryan, has stressed that the stakes at this stage are very high: If the court dismisses all of Mumia's claims, "we would be looking down the barrel of an execution and need to petition the United States Supreme Court." The Court could, however, order a retrial or send the case back to the lower courts.<p>
Meanwhile, Mumia continues to battle on other fronts. Although federal judge William Yohn overturned Mumia's death sentence in 2003, Pennsylvania District attorney Lynn Abrams appealed the ruling. As a result, Mumia remains on death row.<p>
If Abrams' appeal is successful, a new execution date will be set. Finally, Philadelphia DAs made the highly suspect move of asking the entire Third Circuit Court to recuse themselves on the grounds that Gov. Ed Rendell, whose wife serves on the court, was district attorney during Abu-Jamal's 1982 trial.<p>
"The prosecution seems intent on doing just about anything to avoid...a new trial not riddled with racism," said Bryan. "This court, the Third Circuit, has a reputation for being fair and evenhanded, much more than some of the other courts. That is all Mumia and I want: fairness."<p>
Outside the courts, police are trying to undermine Mumia's supporters. A recent fundraiser at a Manhattan club was forced to relocate because of a campaign of harassment from NYPD and Philadelphia's Fraternal Order of Police. An April 24 birthday celebration in Philly with actor Danny Glover and poet Sonia Sanchez was relocated in the face of intimidation.<p>
The Third Circuit Court of Appeals will hear arguments on May 17, and activists will gather in Philadelphia outside the courthouse at 8:30 a.m.<p>

