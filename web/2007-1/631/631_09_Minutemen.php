<font face="Times New Roman, Times, serif" size="5"><b>Menaced by the Minutemen</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Michele Bollinger</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 11, 2007 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">A REPORTED member of the Herndon, Va., chapter of the anti-immigrant Minutemen, was arrested and charged with assault at the May 1 march for immigrant rights in Washington, D.C.--and was soon discovered to be armed to the teeth, with plans to possibly attack immigrants and activists that very day.<p>
Tyler Froatz was arrested for assaulting one of the march's female organizers. Police then found weapons in his bag and car. Froatz had brought to the march a 100,000-volt Taser gun, knives, a claw hammer, a flare gun, a loaded .30 caliber rifle, and a map with details of the park, including sight lines for firing a weapon.<p>
The next day, even more weapons were found in Froatz's apartment--including more than two dozen guns, thousands of rounds of ammunition, a Molotov cocktail and a grenade launcher. When it was discovered that Froatz was a member of the Minutemen--identified as such in a May 3 article in the <i>Washington Times</i>--the chilling pieces began to fall into place.<p>
Organizers of the demonstration confronted Froatz after his first attempts to interfere with the protest. He began by verbally abusing participants and telling Latinos to "go back to [their] home country."<p>
Froatz kept posting barbaric flyers that seemed to be straight out of a Minutemen handbook--including one that read "the only way to stop a flood...is to cut off the flow," with a drawing of U.S. soldiers mowing down immigrants crossing the U.S.-Mexico border, including an image of a pregnant woman being shot.<p>
Froatz tried to provoke a physical confrontation with demonstration organizers. But the organizers--unaware of the arsenal Froatz possessed--argued back at him, making it clear that he was not welcome, and called the Park Police to enforce the demonstration's permit. Froatz was taken into custody.<p>
"If we hadn't confronted him in the way that we did, this man could very easily be free, and a serious tragedy could have unfolded on May 1," said David Thurston, a march organizer and activist in the D.C. Committee for Immigrant Rights.<p>
Instead, however, over 1,000 people marched for immigrant rights, demanding an end to raids and deportations, legalization for all, and that Washington become a sanctuary city.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">SINCE FROATZ is white and not Arab or Muslim, this case has not generated nearly the national media attention it warrants. Yet his is a textbook case of homegrown domestic terrorism--an all-too-real threat in the context of the brutal Immigration and Customs Enforcement (ICE) raids around the country, and the racist violence of vigilante groups like Froatz's Minutemen.<p>
Not surprisingly, the Virginia Minutemen are denying any association with Froatz. But the Minutemen have always tried to divert attention from the true nature of their politics and activities, presenting themselves as a "legitimate" organization.<p>
Thus, George Taplin, president of the Herndon Minutemen, told one TV news reporter, "We do not practice any kind of confrontational activity whatsoever." But there are countless examples to the contrary--from the Minutemen's hate speech to their vigilante "border patrols" where they attack immigrants.<p>
The Herndon Minutemen have been central to getting anti-immigrant legislation passed in Virginia, and to whipping up hatred toward day laborers in particular.<p>
Froatz's connections to the group should be investigated and publicized--so that the public can be aware of the dangers that right-wing hate groups present. The extent to which his inclination for racist violence was nurtured or supported by local Minutemen should be found out.<p>
Froatz may be just one individual, but D.C. area activists shouldn't underestimate the threat of the far right. Recently, an arsonist attacked a day laborers' center run by the immigrant rights group CASA of Maryland in the town of Derwood.<p>
While the far right must be confronted, we must also challenge the daily attacks on immigrants in this country from so-called "legitimate" forces--from ICE raids to the regular denunciation of "illegals" on Lou Dobbs' television program--that give confidence to fanatics like Froatz.<p>

