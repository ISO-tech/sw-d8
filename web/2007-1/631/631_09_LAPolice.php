<font face="Times New Roman, Times, serif" size="5"><b>Outrage at LA cops' May Day assault</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 11, 2007 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">SWINGING CLUBS and firing dozens of rounds of "less-than-lethal" rubber bullets, officers of the Los Angeles Police Department (LAPD) terrorized some 15,000 participants in a peaceful May Day rally for immigrant rights in historic MacArthur Park.<p>
This was the second immigrant rights rally of the day, with more than 50,000 people marching through downtown Los Angeles earlier that afternoon.<p>
The LAPD has a long history of violently suppressing peaceful protests, but this assault made national news when the cops' indiscriminate use of violence led to attacks on several journalists reporting on the rally.<p>
"I was dumbfounded," said Patricia Nazario, a reporter for local radio station KPCC. "I've covered riots. I've covered chaos. I was never hit or struck or humiliated the way the LAPD violated me yesterday."<p>
Nazario was struck twice by LAPD batons while attempting to retreat from the police lines. The second blow to her left leg knocked her to the ground. "I was shocked, trying to scramble to my feet," she said. "At that point, I just started crying...I just felt totally vulnerable."<p>
Nazario was one of at least nine reporters injured by police. A live report on the rally by the Telemundo network was interrupted when police attacked both reporter Carlos Botifoll and news anchor Pedro Sevcec.<p>
The police assault on reporters was a blatant violation of a 2002 court ruling upholding the rights of journalists to remain at the scenes of civil disturbances even after police declare an "unlawful assembly." This ruling was a result of a lawsuit by several journalists who were injured while reporting on the protests outside the 2000 Democratic National Convention--which were also violently attacked by the LAPD.<p>
Predictably, the police are trying to blame a handful of "anarchist provocateurs" for the violence.<p>
But that doesn't explain why the LAPD arrived at the peaceful rally in full riot gear. No "threat" to the helmet- and armor-clad officers has been confirmed, other than the tossing of a few plastic water bottles towards police lines.<p>
Los Angeles teacher and <i>Socialist Worker</i> contributor David Rapkin was an eyewitness to the events. "The police were spoiling for a fight," he said. "They demanded that a small group of young protesters clear a street that had already been blocked off. The protesters understandably resisted this arbitrary order, which the cops used as an excuse to violently shut down the whole rally."<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">ALMOST THREE hours before the protesters' permit expired, the phalanx of LAPD riot cops cleared everyone from the park. As both professional and amateur video footage of the incident clearly shows, not only journalists, but children, the elderly, entire families and even homeless people living in the park felt the blows of police batons and rubber bullets.<p>
The images of Latino families fleeing for safety from advancing lines of riot cops were reminiscent of the Chicano Moratorium march in 1970, when 30,000 protested in East LA's Laguna Park (now Salazar Park) against the Vietnam War.<p>
The LAPD violently suppressed the rally, culminating their assault with the murder of <i>Los Angeles Times</i> reporter Rub&eacute;n Salazar, famous for exposing the LAPD's record of brutality and terror against Latinos.<p>
Faced with indisputable video evidence of police misconduct, LAPD Chief William Bratton and Mayor Antonio Villaraigosa have gone into damage control mode. "I feel comfortable apologizing," Bratton said at a meeting of reporters. "Things were done that shouldn't have been done. This was my best, and that was what was extraordinarily disturbing about this."<p>
Bratton is promising investigations by the LAPD itself and the supposedly independent Police Commission. But such investigations in the past have consistently whitewashed numerous cases of LAPD brutality.<p>
Many activists and community leaders are speaking out against this outrage, calling for an independent investigation. "When we see the police shoot rubber bullets at members of the media and families sitting at picnic benches," wrote A.J. Duffy, president of United Teachers Los Angeles, in a statement released the day after the rally, "when the media and families with children are herded out of a park like cattle, and then attacked when they don't move quickly, something has gone seriously wrong and must be corrected."<p>
The cops who ordered and carried out this disgusting assault on peaceful protesters and reporters should be fired, and their victims deserve compensation for the injuries and terror they experienced.<p>
But in this latest in a long history of LAPD outrages, justice will be denied unless we continue to organize and hold city officials' feet to the fire.<p>

