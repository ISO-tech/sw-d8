<font face="Arial, Helvetica, sans-serif" size="2"><b>EDITORIAL</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Two-faced Washington moralizers</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 11, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">AT LEAST one Bush administration official has been taken down in the latest Washington sex scandal.<p>
Deputy Secretary of State Randall Tobias resigned after he was exposed as a client of a D.C.-area escort service. Though he says nothing illegal happened, Tobias admitted to having "gals" come over to give him "massages"--saying in an interview that "it was like ordering pizza," according to ABC's Brian Ross.<p>
But as gross as his comments are, what's even more outrageous is the rank hypocrisy the revelations about him exposed.<p>
As the U.S. director of foreign assistance and administrator of the U.S. Agency for International Development (USAID)--as well as in his previous job as ambassador for George Bush's Emergency Fund for AIDS Relief--Tobias has made it his job promote ineffective abstinence-only policies and oppose preventative measures like the use of condoms.<p>
Tobias was also instrumental in enforcing a 2003 Bush administration edict requiring recipient countries to swear they are opposed to prostitution before they receive aid--despite the fact that many AIDS activists across the globe say the demonization of sex workers prevents the adoption of safer-sex practices that can help halt the spread of the disease.<p>
According to the <i>Boston Globe</i>, many AIDS groups "expressed concern that the U.S. policy was so broad--and applied even to their private funds--that it would obstruct their outreach to sex workers who are at high risk of transmitting the AIDS virus."<p>
The Brazilian government alone lost $40 million in grant money, because it refused to abandon a highly successful campaign persuading sex workers to use condoms. Elsewhere, programs that provided aid and education to the children of sex workers also ran afoul of the policy.<p>
Meanwhile, recent reports from both the Government Accountability Office and the Institute of Medicine show that the abstinence-only programs backed by Tobias and the rest of the Bush administration are a failure.<p>
As <i>Globe</i> columnist Ellen Goodman commented, "The price [Tobias will] pay for his 'gals' is humiliation. But the biggest flaw in the world of this president and his enablers is not hiding their eyes from human frailty. It's refusing to face facts."<p>

