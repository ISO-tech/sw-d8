<p><a name="War"></a><font face="Times New Roman, Times, serif" size="4"><b>Marching against the war</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Shane Johnson</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CINCINNATI--On May 7, 40 people attended a march at the University of Cincinnati called by UC Students United for Peace/Campus Antiwar Network. The event was part of a week of activities that included the showing of the film <i>Why We Fight</i> and a die-in to symbolize the destruction caused by the U.S. occupation of Iraq.<p>
The march was spirited, with chants such as "Recruiters lie, soldiers die!" The march was deemed "illegal" by campus officials after they supposedly "lost" the scheduling request, but protesters marched anyway.<p>
The group plans to build on the momentum and broaden the struggle to demand immediate withdrawal of U.S. troops.<p>

