<a name="Vermont"></a><font face="Times New Roman, Times, serif" size="4"><b>Vermont Campus Antiwar Network</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Mary Howland</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BURLINGTON, Vt.--Some 50 students from six universities from across the state came together for the first Vermont Campus Antiwar Network (CAN) conference on May 5. The day started with a die-in on a main downtown street.<p>
The protest was followed by workshops on issues such as the case for immediate withdrawal from Iraq, guerrilla theater, radicalism in film and how to build a CAN chapter. The day was full of political debate and discussion, with activists trying to figure out how to build larger antiwar groups on their campuses, and hashing out the answers to big political questions, like the nature of U.S. imperialism.<p>
The evening ended with a plenary titled "Iraq, Iran and the Future of the Antiwar Movement." Drew Cameron, a member of Iraq Veterans Against the War (IVAW) and a student at the University of Vermont, spoke about the case for immediate withdrawal from Iraq from the perspective of a veteran.<p>
Siham Elhamoumi spoke as a student activist from St. Michael's College, urging students to continue the fight against the occupation. Saman Sepehri, a contributor to the <i>International Socialist Review, </i>laid out the nature of U.S. interests in the Middle East, and why Iran is the next target of U.S. imperialism.<p>
 The final speaker was an active duty-soldier, a member of IVAW who has already served one tour in Iraq. He spoke of his experiences in Iraq, why he is against the war, and how student activism can give the needed support to active-duty soldiers to resist the war. As he said, "Soldiers need the solidarity of a civilian movement to give support to the development of a full-fledged GI resistance."<p>
The conference was a beginning step in solidifying a statewide network of student antiwar groups Attendees were inspired and excited to continue the fight and the organizing through the summer and into the coming fall semester.<p>
