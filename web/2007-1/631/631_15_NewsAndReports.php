<p><a name="Minutemen"></a><font face="Times New Roman, Times, serif" size="4"><b>Protesting the Minutemen</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Sarah Knopp</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BALDWIN PARK, Calif.--Approximately 40 members of the anti-immigrant organization "Save Our State" (SOS)--a sister group of the racist Minuteman Project--showed up to harass residents of the city of Baldwin Park on May 5 in response to the city council's recent vote to build a day laborer center at the Home Depot.<p>
Outrageously, many of the racists carried signs saying "We support the [Los Angeles police department]," a reference to the police brutality unleashed against immigrant families, activists, reporters and lawyers at recent the May Day march.<p>
SOS was surprised, however, when 400 people--mostly residents of Baldwin Park--showed up chanting "Racists go home!"<p>
Outnumbered 10-to-1, SOS made sure they had over a hundred riot police to protect them and allow them take over the lawn of the city council and taunt the community.<p>
After police escorted them to their cars, residents and activists organized an impromptu speak-out, and people signed up to help with emergency responses to any kind of repression--whether from the Minutemen, immigration raids or police brutality.<p>
As one Baldwin Park woman commented, "Thank you Minutemen for getting us organized--and thank you everyone for coming out to support us."<p>
