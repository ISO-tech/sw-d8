<p><a name="Saigon"></a><font face="Times New Roman, Times, serif" size="4"><b>Saigon Grill</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Geoff Bailey</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--More than 150 students from six campuses took part in a sit-in to support locked-out delivery workers at the Saigon Grill.<p>
Students entered one of restaurant chain's three locations in small groups. At a pre-arranged time, stood up on the tables, donned red T-shirts and began chanting, "Two dollars, no way! What do we want? Fair pay!"<p>
A short statement was read and chanting continued for almost an hour, shutting down the restaurant until the police arrived and threatened arrest, at which point student marched outside to join the workers and other supporters.<p>
The 36 delivery workers at Saigon Grill have been locked out since they began protesting low pay and unsafe working conditions. Workers at the restaurant were being paid as little as $1.60 an hour and were verbally and physically abused by the management. They reported being sent repeatedly into buildings where they were mugged, only to be told by management that they had to reimburse the restaurant for the lost orders.<p>
The three locations of the Saigon Grill, meanwhile, make $2 million per month in profit. When workers, organized through the Chinese Staff and Workers' Association, threatened a lawsuit, management responded by locking them out.<p>
"I gave more than 10 years of my life to this restaurant. The more money the owners made, the more they stole from us," said one of the locked-out workers.<p>
Anthony Hang, a freshman at Hunter College, stressed that students should see this as their struggle as well. "I think it's important for students to realize that these are conditions that working students can face," says Hang, who worked as a part-time busboy last fall.<p>
Pickets will continue and more actions are planned until the workers are rehired and their wages raised.<p>
"When we started, it was very difficult," Yu Guan Ke, one of the locked-out workers, told <i>Socialist Worker. </i>"Little by little, other coworkers and delivery workers joined us because they faced the same conditions at their workplaces. We have overcome the hardest part, because now we know that all workers will come out and support and fight together for justice."<p>

