<a name="Woodfin"></a><font face="Times New Roman, Times, serif" size="4"><b>Woodfin Hotel</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jessie Muldoon</b></font><p>

<font face="Times New Roman, Times, serif" size="3">EMERYVILLE, Calif.--"We are immigrants, not criminals," Guadalupe Gonzalez, speaking through a translator, told a crowd of over 300 activists who came to protest the firing of 12 Woodfin Hotel employees on April 27.<p>
The Woodfin Hotel has stubbornly refused to comply with a living-wage law that would improve wages and working conditions for hotel employees. All other major hotels in Emeryville have complied, and the workers at the Woodfin have been active and outspoken in their demand that their employer do the same.<p>
Since the living-wage law came into effect, the East Bay Alliance for a Sustainable Economy, a grassroots activist organization that led the initiative for the living-wage law, has led the campaign for justice at the Woodfin. Activists and workers have picketed, lobbied and pressured the Woodfin's corporate clients.<p>
In December 2006, more than 20 workers were fired, but through activism and community support, were able to win their jobs back. In April, 200 supporters marched through the small city of Emeryville to alert the community and put pressure on the City Council to support the workers' demands for a fair wage.<p>
In apparent retaliation, just days before the massive May Day marches, the Woodfin fired 12 employees who had been involved in the pickets and organizing efforts.<p>
The Woodfin may have been counting on the recent ICE raids to scare the workers from fighting back, but instead, it seemed that the rallies buoyed support for the workers at the hotel. More than 300 people rallied, and more than 40 participated in a civil disobedience, including labor leaders, faith leaders and local politicians.<p>
The struggle at the Woodfin isn't over, and activists will continue to put pressure on the Woodfin to abide by the living-wage law.<p>
