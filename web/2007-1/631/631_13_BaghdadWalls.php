<font face="Times New Roman, Times, serif" size="5"><b>U.S. walls will turn Baghdad into a prison</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 11, 2007 | Page 13</font><p>

<font face="Times New Roman, Times, serif" size="3">THE U.S. military in Iraq has unveiled a new strategy to suppress "sectarian violence" wracking Baghdad: building border walls.<p>
Soldiers from the 407th Brigade Support Battalion have begun building a wall, replete with razor wire, border guards and fortified checkpoints. The concrete wall will run three miles, stand 12 feet high, and ultimately enclose and "protect" 10 Sunni neighborhoods in the community of Azimaya.<p>
Rather than an outpouring of jubilation at their newfound security, Sunni residents remonstrated against the wall, demanding the suspension of its construction. "This will make the whole district a prison," said Ahmed al-Dulaimi, a 41-year-old engineer who lives in the area. "This is collective punishment on the residents of Azamiya."<p>
Shiites "on the other side" also decried the plan, prompting Iraqi Prime Minister Nouri al-Maliki to order the project to be halted.<p>
In the mock opera of "Iraqi democracy," the U.S. occupiers don't even feign deference to their colonial subjects. "We will continue to construct the security barriers in the Azamiya neighborhood," said Iraqi Brigadier Gen. Qassim al-Moussawi at a news conference, under the watchful eye of his U.S. patr&oacute;n, Rear Adm. Mark Fox. "This is a technical issue."<p>
In a verbal twist that would make the logicians of the Bush administration nod in appreciation, he concluded, "Setting up barriers is one thing and building barriers is another. These are moveable barriers that can be removed."<p>
Despite Maliki's choreographed indignation, concrete barriers, blast walls and razor-wire already disfigure communities across Baghdad's landscape--serving as monuments to the violence of the occupation and its failure to win "the hearts and minds" of the Iraqi people. According to another Iraqi, Khalil al-Obaidi, "The real reason behind this wall is to increase people's sufferings and complicate their daily lives."<p>
Rather than a defensive or protective strategy, the construction of fortified barriers and border walls is an offensive strategy to police the people. It aims to physically reduce the Iraqi population into smaller, more controllable units, with freedom of movement further constricted and determined by the occupier.<p>
This demonstration of power is designed to collectively punish civilian communities in territories that are considered "lost to the enemy," and to attrite active and passive support for the resistance through forms of low-intensity warfare.<p>
This strategy includes physical and psychological means to weaken resistance: frequent and arbitrary searches; detentions and arrests at crossing points; and restriction of movement to work, the marketplace or other daily requirements, and to visit friends and relatives.<p>
By targeting the whole community, the occupiers hope to isolate the resistance and reduce the population to a state of dependence and resignation, a desperate strategy for a failing occupation. In a city where unemployment afflicts at least half of the population, such restrictions on movement will only intensify the misery index for ordinary Iraqis and add new ranks to the resistance.<p>
It is in the nature of military occupation that subject peoples are made to pay for their "failure to comply." Ultimately, the occupiers may find that they are the ones being walled in by their own policies.<br>
<b>Justin Akers Chac&oacute;n</b>, San Diego<p>

