<font face="Times New Roman, Times, serif" size="4">South Dakota right-wingers draft new ban on abortion</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Targeting women's rights again</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 9, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">SOUTH DAKOTA'S anti-abortion right is getting ready to try to take away a woman's right to choose--for the second time.<p>
In November, voters overturned a South Dakota law banning all abortions except when the life of the mother was in jeopardy. With polls showing that many voters would have supported the law if it also included exceptions for rape or incest, politicians last week reintroduced the bill in a slightly modified form that includes those exceptions.<p>
But to be "eligible" to obtain an abortion, rape victims would be required to report rapes to police within 50 days of the crime, and submit to an examination. According to the Associated Press, "Doctors would have to confirm the report with police and would have to take blood from aborted fetuses and give that information to police for DNA testing."<p>
Victims of incest would not only be forced to give their doctors permission to report the crime, they would also be required by law to reveal the identity of their attackers. Blood samples from fetuses would also be provided to police for DNA evidence.<p>
Even with these draconian "exceptions," abortions could only be legally performed before the 17th week of pregnancy. Doctors who perform an abortion after the 17th week, or who perform an abortion not related to a case of rape, incest, or to preserve the life of the mother, would face 10 years in prison.<p>
If passed, the law would doubly victimize rape and incest survivors, once by the crime itself and again by requiring their decision on reporting the crime or identifying their attacker.<p>
This newest attempt to restrict abortion rights in South Dakota has nothing to do with "protecting" women who are survivors of rape or incest. It is about making sure that women don't have the right to control their own bodies.<p>

