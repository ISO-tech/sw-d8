<a name="resist"></a><font face="Times New Roman, Times, serif" size="4"><b>Support war resisters</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Gillian Russom</b></font><p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES--On February 3, more than 300 people marched in support of war resisters Ehren Watada and Agust&iacute;n Aguayo in LA's Little Tokyo neighborhood.<p>
The event was organized by Nikkei for Civil Rights and Redress, founded in 1980 to fight for reparations and justice for victims of the Japanese internment, and the Asian American Vietnam Veterans Organization.<p>
The event was scheduled to protest the trial of Army Lt. Ehren Watada--the first officer to speak out publicly against the war in Iraq--which began February 5. Aguayo, a conscientious objector who went AWOL after the military denied his CO status, faces a court-martial in Germany on March 6 and could face up to seven years.<p>
Protesters marched through Little Tokyo, chanting, "Free Watada! Free Aguayo! Drop the charges now!" The lead banner was carried by Watada's mother Carolyn Ho and members of Aguayo's family, including his wife Helga, his mother and two daughters.<p>
Protesters packed the basement of a local Buddhist temple to hear speakers, musicians and spoken-word artists. Speakers included Vietnam veteran Michael Nakayama and Jabbar Magruder of Iraq Veterans Against the War.<p>
In a moving speech, Helga Aguayo explained how her husband came to oppose the war before he deployed. During his training, he was asked to shoot at targets using live ammunition, some that were designated "insurgents" and some "friendlies."<p>
"'Oh my God,' he said, 'I have to choose who to kill. There's something deeply wrong with this,'" she explained. "He couldn't do it, he wouldn't do it, and he won't do it...Through their resistance, it's soldiers like Ehren and my husband who are going to end this war."<p>
"He told me, 'It's a matter of conscience,'" said Carolyn Ho, describing her son's decision to refuse. "'When I sit in the jail cell at the end of the day, I will know truly that I have not engaged in war crimes, I have not killed innocent men, women, and children.' He wasn't just talking about the over 3,000 Americans killed and over 23,000 injured. He was talking about the over 650,000 Iraqis [killed]."<p>
These resisters and their families are galvanizing the antiwar movement with a new sense of how the actions of ordinary people could ultimately stop the U.S. from prosecuting its wars. We must support them.<p>
<i>To find out more about Agust&iacute;n's case, visit <a href="http://www.aguayodefense.org" target="_blank">www.aguayodefense.org</a>. To make a donation to his legal defense, visit <a href="http://www.couragetoresist.org/donate" target="_blank">www.couragetoresist.org/donate</a>. For more information on Ehren's case, visit www.thankyoult.org</i>.<p>
