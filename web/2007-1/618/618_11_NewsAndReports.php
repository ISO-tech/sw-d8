<p><a name="immigrant"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend immigrant rights</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Zach Mason and Chris Yarrison</b></font><p>

<font face="Times New Roman, Times, serif" size="3">RICHMOND, Va.--In response to a barrage of anti-immigrant measures passed in the Virginia House of Delegates, and with more on the way, activists organized four days of actions in protest.<p>
Actions to oppose anti-immigrant legislation were organized by Mexicanos Sin Fronteras (MSF), a network of day laborer organizations in the Virginia, Maryland and Washington, D.C., area, and their supporters.<p>
For the sponsors of the legislation, the goal is nothing less than driving undocumented workers out of the state. "The ultimate goal is to make the commonwealth of Virginia an unwelcome place if you are in this country illegally," said Del. Jackson Miller, the sponsor of a bill that passed the Virginia House January 30, which proposes cutting off state and local funds to groups that give aid to undocumented immigrants.<p>
Events began on February 2 at the capitol with a press conference and the beginning of a hunger strike. The next day, more than 100 people rallied in nearby Monroe Park. Speakers included Ricardo Juarez of the Woodbridge Workers Committee and a leading member of MSF. After the rally, protesters marched to the state capitol, chanting "&iexcl;No somos criminales, somos trabajadores!" and "&iexcl;Amnist&iacute;a general!"<p>
Mar&iacute;a Alvarez spoke to <i>Socialist Worker </i>as she walked with her two young children. Her house was raided by Immigration and Customs Enforcement (ICE) agents and her husband detained just weeks ago. "They come in your house with guns," said Mar&iacute;a. "They make you feel like a criminal. Now I don't know what we will do."<p>
In Herndon, Va., a chapter of the racist Minutemen Project successfully encouraged the town to sign up for ICE to train their police in checking people's papers and deportation, and several other cities have followed suit. One of Del. Miller's proposed bills would make that training available statewide.<p>
Groups like MSF are preparing for larger events in this spring.<p>

