<font face="Times New Roman, Times, serif" size="4">Company wants givebacks despite huge profits</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Workers hit the picket line at Harley-Davidson</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 9, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">WORKERS AT Harley-Davidson's plant near York, Pa., defied the company's divide-and-conquer tactics with a strike that began February 2.<p>
The 2,700 workers, members of International Association of Machinists (IAM) Local Lodge 175, voted by 98 percent to authorize a strike over Harley's demand to increase health care costs for current employees and force new hires into a lower-tier pay schedule.<p>
The company has already extracted similar concessions from workers, members of the United Steelworkers, in its Milwaukee engine plant in a contract ratified in November. That deal not only imposed a two-tier pay system and raised health care costs but also cut retiree benefits and cost-of-living adjustments. In return, management promised to invest $120 million in the Milwaukee plant and add 100 jobs there rather than outsource the work.<p>
Now management wants IAM members in the Pennsylvania plant--where 65 percent of Harley motorcycles are assembled--to swallow similar givebacks.<p>
Although the proposed three-year contract contains a 4 percent pay increase in each year, half that raise is contingent on workers' acceptance of the health care plan used by salaried employees--a change that could cost unionized workers thousands of dollars each year. And the two-tier pay system would keep younger workers from ever reaching top pay levels.<p>
Yet Harley certainly has the money to fund both decent raises and maintain health benefits. Worldwide, sales of Harley bikes shot up 9 percent last year. In the fourth quarter of 2006, the company made $252.4 million in profits on $1.5 billion in revenue--an 11.9 percent increase from the same period a year earlier.<p>
Harley's stock price has soared 129 percent over the past five years, giving the company a market value of $18 billion--a large amount for a relatively small company.<p>
As the <i>Wall Street Journal </i>reported, Harley executives seized the opportunity to cash in, selling 1.5 million shares worth $101.3 million, and then using the cash to exercise stock options that enabled them to buy back nearly as many shares for just $55.4 million. "That means the insiders made net profit of $45.9 million, or a gain of 83 percent," the <i>Journal </i>noted.<p>
All this has left workers bitter--particularly since the unions took big concessions in the 1980s when the company was on the brink of bankruptcy. Protectionist legislation also gave Harley advantages over foreign competitors.<p>
But workers have never fully shared in Harley's subsequent prosperity--and now the company wants workers to sacrifice again despite the company's success. "What is being proposed is significant health care cost shifting by a profitable company," Frank Larkin, a spokesperson for the IAM International, told <i>Socialist Worker</i>.<p>
Joe Klyeman, a tool-and-die gauge maker who's worked at Harley for 28 years, is angry about the demands for cuts. "We took concessions here in the '80s, and now they're making billions of dollars, and they want us to pay for our insurance?" he told the <i>York Daily Record. "</i>We made them rich, and now they want us to [make concessions]?"<p>

