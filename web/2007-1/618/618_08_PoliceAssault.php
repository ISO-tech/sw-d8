<font face="Times New Roman, Times, serif" size="4">Arrested for the "crime" of trying not to be choked</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>We need protection from the "protectors"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 9, 2007 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">IN WASHINGTON, D.C., we live in a hostile police climate because of the proliferation of hostile police officers.<p>
This was displayed last August when my friend, Sahale Hammond, pulled into the Kiss and Ride section of the Rhode Island Avenue Metro, with his radio playing, to pick someone up. Police officer Stephen Boehm approached Hammond after his car was positioned in a way that would go against the flow of traffic.<p>
"I didn't have a tail light out, a headlight--there was nothing wrong that would give reason for police to come up," Hammond told me. "He asked me something, but I couldn't hear him over the radio. He asked me again, and I said 'What do you want?'"<p>
This prompted Boehm to reach for Hammond's keys. Hammond's resistance to this propelled Boehm to take hold of Hammond with two hands, choking him. "I said 'Get off me,' but he wouldn't," Hammond said. For seven seconds, all Boehm was concerned with was hurting and assaulting a young, innocent Black man.<p>
The application of deadly force on a traffic stop is egregious enough, but this isn't a new trend. Hammond, fearing a more aggravated injury or worse, decided to drive off while still being strangled. With one hand squeezing Hammond's collarbone, Boehm produced his sidearm from his holster with the other, as Hammond's leaky '88 DeVille slowly sped up.<p>
It was Boehm's decision to be "dragged 20 yards." All he had to do was let go of Hammond, whose only crime here was driving while being Black and being seated unthreateningly.<p>
To inform his family of what had just happened, Hammond drove to the nearest pay phone to call his house. Later, in the jury's eyes, the decision to stay in the area was an indication of Hammond's innocence. This is the moment he was apprehended by one of the various back-up officers in the area called in to identify him.<p>
"Why am I being arrested? What did I do?" Hammond asked. It wasn't until he was in the back of an un-air-conditioned squad car with the windows rolled up in the 90-degree weather that he found out. He was charged with "APO"--assault on a police officer. Boehm then peeked in the window, placed his thumbs behind his ears and belittled him like a second-grader would.<p>
My friend was facing years in prison for the "crime" of trying not to be choked! The hypocrisy of the charges against Hammond was exposed in court when the bruises on his neck were captured in mug shots and his own personal photos. The jury of four whites and eight Blacks recognized Boehm's account for what it was: blatant racial profiling. They acquitted Sahale.<p>
That said, though, it is easy to see that this could have destroyed Sahale's life, or he could have been killed. It speaks to the need to build a struggle to protect us from our "protectors."<br>
<b>Marco Murillo</b>, Washington, D.C.<p>

