<font face="Times New Roman, Times, serif" size="5"><b>Why our schools are in a shambles</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 9, 2007 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">LIKE MANY schools (particularly urban ones) across the country, the Providence School District is in a shambles. The children of Providence are caught between the disastrous, bipartisan No Child Left Behind legislation and a decaying infrastructure. Plus, there has been an alliance between the city and developers in gentrifying the city.<p>
Days after the results of a major school infrastructure review were published, it was announced that the West Broadway Elementary School would be closed. The students would be moved from their neighborhood school to another already occupied school miles away. This school has numerous problems of its own, related to its recent construction on the site of a waste dump.<p>
The neighborhood immediately began organizing through the parent-teacher organization. These organizing meetings included translation and drew a diverse crowd that was consistent with the makeup of the school and neighborhood.<p>
The superintendent held open meetings to explain the situation to parents and is basing the school closure on the safety review of a fire inspector. The key finding is the need for kindergarten and first graders to be able to exit the building without having to go up or down a stairway.<p>
Obviously, this is not a new development for this 103-year-old building. A simple review of the fire code shows that a stairway dedicated to use by these students would be permissible and is available in the school.<p>
Parents met the superintendent at a school board meeting following the closure announcement, where they packed the room with 90 parents, students and teachers. At the meeting, parent after parent confronted the superintendent, and everyone left with the clear feeling that he is not interested in child safety nor in listening in general.<p>
Parental input is tolerated, but clearly not sought. The result is that everyone knows that it's going to be a fight to win a better education for our children. It is clear that this decision is being shoved down the throats of the neighborhood.<p>
Excluding the community from decision-making gives the decision-making power to the politicians and developers. To have their say, the parents have hard work ahead--but they are motivated and united.<br>
<b>Eric Rehder</b>, Providence, R.I.<p>

