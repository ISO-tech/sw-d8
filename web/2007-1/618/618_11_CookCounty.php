<font face="Times New Roman, Times, serif" size="5"><b>Fight against cuts heats up</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Lalasz</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 9, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--Hundreds of health care and other county workers, as well as community members, continued to protest against the Cook County Board's proposed 17 percent budget cuts last week. If carried out, the cuts would include the layoff of 1,492 county employees, the closing of 18 public health clinics and the trimming of nearly half a billion dollars from the budget overall.<p>
On January 29, hundreds of affected workers and community members attended an evening town hall meeting sponsored by Service Employees International Union (SEIU) Local 20 at Malcolm X College, where union members questioned some of the county commissioners. Many made the connection between the cuts in health care and the money being allocated for the occupation of Iraq.<p>
The Cook County Board responded the next day by firing three doctors and the head of nursing at Cermak Hospital, the public health facility that serves the county jail. Those fired--Medical Director Sergio Rodriguez, Associate Medical Director Connie Mennella, Director of Operations Michael Puisis and Muhammed Mansour, a senior physician in charge of infection control--are all nationally recognized advocates for prisoner health care.<p>
As Dr. Robert Simon, the newly appointed head of the Cook County Bureau of Health Services, told the <i>Chicago Tribune</i>, "This is a leadership that in my opinion will be an obstacle to [needed] changes."<p>
These firings are not only about removing people who are opposed to the cuts, but are also about sending a message to other workers that this is what will happen to them if they fight back.<p>
But the firings only enraged the hundreds attending another public hearing on the cuts in Maywood, Ill.--some of whom called for a meeting with County Commissioner Mike Quigley, who never showed up to the hearing.<p>
On January 31, Cook Country Board President Todd Stroger gave an interview to local Chicago radio station WGCI to try to defend his budget cuts, saying that departments are exaggerating the effects. But as one caller shot back, "You're killing the people who actually help the county to run!"<p>
The same day, in an attempt to pass the blame, the Rev. Jesse Jackson tried to shield Stroger from mounting criticism. At a joint press conference, Stroger and Jackson said that Democratic Gov. Rod Blagojevich bears some of the blame for the county crisis because he didn't release enough Medicaid funding quickly enough to help the county health system.<p>
Stroger and Jackson maintain that if the state would speed up--and increase--the share that Cook County gets in Medicaid funding, the majority of a $500 million budget crisis could be averted. They also called for more federal funding for county health programs and concessions by unionized county employees.<p>
We cannot fall for this, since the proposed budget cuts are the idea of Stroger and the Cook County Board, as much as of the state and federal government, none of whom prioritize public health care services for the working poor and uninsured.<p>
We need to keep the pressure up. SEIU Local 20 is calling for a mass action on February 21 at the final Cook County Board meeting before the budget is scheduled to be passed at the end of the month.<p>
The longer Cook County officials continue to collide with significant public outcry, the more they will be forced to scramble to find money to stave off cuts. As Commissioner Joseph Mario Moreno told the <i>Chicago Sun-Times</i>, "There's no other way."<p>
