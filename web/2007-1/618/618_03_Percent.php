<font face="Times New Roman, Times, serif" size="5"><b>How the other 1 percent lives</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 9, 2007 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">TWO ASTONISHING numbers grabbed headlines earlier this month.<p>
One was ExxonMobil's earnings for 2006 of $39.5 billion--the highest corporate profits in world history. In other words, ExxonMobil was making $4.5 million--in profits--for every hour of every day of the last year.<p>
The other number was the personal savings rate for 2006--negative-1 percent. That means the average U.S. household spent every penny of its after-tax income--and then some more, by dipping into savings, if it had any, or more likely, by racking up credit card and other forms of debt.<p>
The only other years that the personal savings rate has fallen into negative territory was 2005 and two years during the worst of the Great Depression.<p>
And because this government statistic makes no distinction between rich and poor households, it disguises just how dire the situation has become for many people in the U.S.<p>
On the one hand, the young and poor find themselves struggling the most to keep their heads above water. Some 42 percent of those between the ages of 18 to 49 said they are likely to spend more than they can afford, and 45 percent of those with household incomes below $30,000 reported the same.<p>
On the other hand, people like Rex Tillerson, the CEO and chair of ExxonMobil, have a hard time spending even a portion of what they made in 2006. Tillerson took home $18.5 million last year--raking in more in an hour than a minimum-wage worker makes in a year.<p>
In short, the households with the debt are different than the households with the cash, according to economic analyst Stephanie Pomboy.<p>
The richest 1 percent of households hold 30 percent of total assets and 7 percent of total debt, while the bottom 50 percent of households have just 6 percent of assets and a staggering 24 percent of the debt.<p>
The obscene gap between rich and poor even merited a mention from George W. Bush during his recent trip to Wall Street.<p>
Bush was greeted like a rock star when he walked onto the floor of the New York Stock Exchange, but his words of caution about skyrocketing CEO pay met with tepid applause. "The salaries and bonuses of CEOs should be based on their success at improving their companies and bringing value to their shareholders," Bush declared.<p>
But the president ultimately had reassuring words for his audience, saying that the government shouldn't set limits on CEO pay--and that it's best to "respond to the income inequality we see with policies that help lift people up," instead of "tear others down."<p>
Don't hold your breath waiting for that plan to "lift up" workers' pay.<p>

