<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">April 20, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Washington state mental health workers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Brian Huseby</b></font><p>

<font face="Times New Roman, Times, serif" size="3">OLYMPIA, Wash.--Approximately 350 community mental health workers, members of Service Employees International Union (SEIU) 1199NW, and their supporters rallied and marched at the capitol to demand that the Washington legislature provide funding for adequate pay raises for workers in state-funded community mental health facilities.<p>
The rally and march were accompanied by a one-day strike of 1,000 workers in Western Washington, also organized by SEIU 1199NW.<p>
Workers are demanding that the legislature include $20 million in the biennial budget to provide the raises. The current projected budget only includes $7.5 million to 10 million for the raises--yet could provide millions more to build a new sports arena for the Seattle Supersonics.<p>
Community mental health workers can earn $4,000 to $25,000 more per year working in the private sector. This, and a lack of affordable health care, resulted in a 33 percent turnover of staff last year. "It's hard to survive on the wages we're getting," Jeff Nogler, a mental health assistant in Olympia, said in an interview.<p>
The staffing crisis is taking a tremendous toll on the largely poor clients of community mental health facilities. "Often what we'll see is more people ending up in hospitals and jails because the caseworker cannot keep track of them", said Shannon Deighan, another mental health worker.<p>

