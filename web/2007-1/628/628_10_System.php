<a name="Polluters"></a><font face="Times New Roman, Times, serif" size="4"><b>Putting polluters' interests first</b></font><p>

<font face="Times New Roman, Times, serif" size="3">DOES THE P in "EPA" stand for "pretend?"<p>
Thanks to recent changes in the Environmental Protection Agency's system for reporting the release of toxic chemicals, U.S. residents will get to live next to millions of pounds of new chemical waste this year.<p>
The revisions implemented by the EPA sharply raised the threshold where companies must report how much toxic material they release or dispose of. According to the Environmental Working Group (EWG), data about nearly 600,000 pounds of chemicals that would have been released under the old rules will now be exempt.<p>
The Toxics Release Inventory (TRI) has been operating for the past 20 years and requires approximately 23,000 facilities across the country to reveal how they handle waste--which has allowed community groups and states to hold polluters responsible for damaging impacts on the environment.<p>
But the new regulations, issued in December, raise the threshold that triggers detailed reporting on releases of most chemicals covered by the TRI, from 500 to 2,000 pounds per year. Facilities that don't reach the threshold could just list basic information identifying the chemicals released.<p>
The EPA says that the change will affect less than 1 percent of all waste tracked by the TRI nationwide--and is designed to offer polluters "burden relief" by reducing the time and money spent on complying with environmental regulations.<p>
But according to the EWG, in California alone, at least 274 waste-management facilities will benefit from the change, including 52 that would no longer be required to report any details of how they handle toxic waste--including an estimated 12,000 pounds of benzene, a cancer-causing agent, and more than 26,000 pounds of toluene, which has been linked to neurological disorders.<p>
The change, according to EWG Vice President Bill Walker, will be "a real blow to the rights of Americans to know what toxic chemicals are being used or released to their neighborhoods."<br>
-- <b><i>The New Standard, </b></i>April 12, 2007<p>
