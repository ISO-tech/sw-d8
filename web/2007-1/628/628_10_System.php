<p><a name="Abstinence"></a><font face="Times New Roman, Times, serif" size="4"><b>Abstinence-only runs into reality</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEWS FLASH: Telling kids not to have sex doesn't work. And now, even Congress knows it.<p>
According to a study by Mathematica Policy Research, Inc., students who took part in sexual abstinence programs were just as likely to have sex as those who did not. In addition, those who attended one of the four abstinence classes that were reviewed reported having similar numbers of sexual partners as those who did not attend the classes. And they first had sex at about the same age as other students.<p>
The federal government now spends about $176 million annually on abstinence-until-marriage education.<p>
Not surprisingly, Bush administration officials cautioned against drawing sweeping conclusions from the study, saying the programs reviewed were flawed because they were some of the first established after Congress overhauled welfare laws in 1996.<p>
Instead of questioning whether abstinence-only education works, administration officials said the real problem is that there isn't enough abstinence education--and that the "abstinence message" should be reinforced in subsequent years to truly affect behavior.<p>
 "This report confirms that these interventions are not like vaccines," said Harry Wilson, the commissioner of the Family and Youth Services Bureau at the Administration for Children and Families. "You can't expect one dose in middle school, or a small dose, to be protective all throughout the youth's high school career."<br>
-- <b>Associated Press, </b>April 13, 2007<p>
