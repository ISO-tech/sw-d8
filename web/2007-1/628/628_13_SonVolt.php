<font face="Times New Roman, Times, serif" size="5"><b>Son Volt's <i>Search </i>for a better world</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Alexander Billet</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 20, 2007 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b>Son Volt, </b><i><a href="http://www.amazon.com/gp/redirect.html?ie=UTF8&location=http%3A%2F%2Fwww.amazon.com%2FSearch-Son-Volt%2Fdp%2FB000MNOXXA%3Fie%3DUTF8%26s%3Dmusic%26qid%3D1176869858%26sr%3D8-1&tag=socialistwork-20&linkCode=ur2&camp=1789&creative=9325" target="_blank">The Search</a></i>, Legacy Records, 2007.</font><p>

<font face="Times New Roman, Times, serif" size="3">IT IS hard to listen to Son Volt's new album <i>The Search</i> without asking one obvious question: What exactly is the group "searching" for?<p>
Ultimately, it's something that has preoccupied frontman Jay Farrar ever since his days with Uncle Tupelo, when he and his bandmates were dubbed the crown princes of "alternative-country" (a label he's attempted to distance himself from recently).<p>
Farrar has always been masterful at weaving stories of working-class life, of ordinary people doing what they can to survive, but somehow never forgetting their dreams.<p>
Furthermore, Farrar's thoroughly humanistic approach flies in the face of the blue-collar-equals-red-state formula that we're told is part of America's genetic makeup. When the image of both "middle America" and country music is built around conservative values and Larry the Cable Guy, Son Volt's eloquent voice, backed by a steady rock rhythm and confidently raucous guitars, shakes that myth to its core.<p>
In this respect, <i>The Search </i>is a bold step for Son Volt. Some critics have panned the album's experimentation. The realm of "traditional American rock," or "Americana," is largely regarded as static, with little room for innovation. But Farrar began innovating here with Tupelo, and continues bucking the trend on such tracks as "Circadian Rhythm," mixing The Band-era organ arrangements with a trippy backward guitar loop.<p>
This dichotomy, between old and new, past and present, is what characterizes <i>The Search</i>. In so doing, it shows that the stories of ordinary people haven't gone anywhere. Like Son Volt's music, they have only evolved and are more potent than ever.<p>
In "The Picture," Farrar incorporates a rollicking Memphis-style horn section into an anthemic rock background, ruminating on the present state of things "when war is profit and profit is war." Farrar is even more explicit and urgent on the track "Underground Dream," which takes on what he calls "conservative cowboy ideologies" and openly questions a society that chooses war over education.<p>
But Farrar's greatest talent, crafting vivid tales of regular people looking for a place to belong, remains unchanged. This is the strong suit of any music with its roots in folk or country, and Farrar, as always, does not disappoint.<p>
The gentle yet haunting "Methamphetamine" tells the story of a man trying to make end's meet and lamenting his dreams cut short: "I took the night shift, another nickel on the dime/Try to play it straight, make it different this time/Still waiting to meet the next ex-wife...It's either watching these gauges for Monsanto/Or a bar-back job at the casino/The Army won't want me after what this body's been through."<p>
The song's chorus is the man's longing to be taken back to his home in North Carolina, a home that doesn't exist anymore.<p>
Perhaps this dream deferred that keeps Farrar up at night is also what he is searching for. If so, then it's a search that runs through the history of American music--from Woody Guthrie, Hank Williams and Johnny Cash to Bob Dylan, Joan Baez and Bruce Springsteen.<p>
It's a search for a world where ordinary people's hopes and aspirations matter and can come true. That's a world well worth searching for.<p>

