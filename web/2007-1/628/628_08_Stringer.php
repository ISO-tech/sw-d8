<font face="Times New Roman, Times, serif" size="5"><b>"What people saw in these 10 young ladies"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 20, 2007 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">Rutgers women's basketball coach C. VIVIAN STRINGER spoke out about the reaction against Imus on the MSNBC show "Countdown with Keith Olbermann." These are excerpts from her comments.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">ALL OF us have been saying all along that, whether it's a garbage truck driver or a cameraman or a politician or a president, we're all equal--we deserve respect and human dignity.<p>
I think that people were able to separate that this wasn't about Rutgers women's basketball as much as it spoke to the broader issues: It spoke to the degradation of women. Every one of us has a mother, and it had to be put in that perspective.<p>
I think that's what touched us. We've become so desensitized that we've allowed a lot of things to pass. Unfortunately, we, the people, haven't understood that too often politicians and religious leaders speak for us, and we sit back and don't realize the power in numbers--and when to say enough is enough.<p>
I really think that this was reduced to the very human element of decency--to make sure that these kinds of things are stopped. I hope that it doesn't stop with Mr. Imus, because he's not the only culprit. I think that some of us as adults and as parents are responsible to some extent, because we haven't stepped up--about the fact that the corporate executives have dealt with the color of all this being green.<p>
We see these things play out over time. A kid steals something with a plastic cap pistol and spends 10 years in jail, and yet you see white-collar thieves who steal millions of dollars. I think we've just got to come back to some level of human decency. And I do think that if people stood up, politicians wouldn't wait for a poll, but would be strong enough to make a decision and stand up.<p>
What people saw is these 10 young ladies who had done nothing wrong, but had been stellar students in the classroom and had a great story to tell about the fact that it doesn't matter where you come from, but how you finish.<p>
This team lost by 40 points to the number one team in the country on national television in first game of the season. But you know what? Six months later, they defeated the number one team in the country. That was the story, and it was such a special story, and a glorious moment. This is something that never should never have been forgotten.<p>
But instead, they find themselves coming back to defend themselves against these derogatory remarks. I think that's what touched people--because they were able to see them not in a basketball uniform, but as their daughters and their nieces and their grandmothers and their mothers. And people began to say, "Where is our sense of human decency?" Tell me anyone who can listen to this and insulate themselves from the hurt that was thrust upon these young ladies.<p>
It crossed everything. It was sexist, and it was biased, and it was racist. And aside from that, it belittled the great game that took place. Did anybody even know that Tennessee just now won a championship--that Rutgers and Tennessee had played? His reference was lessening the talents and the skills and the hard work of all these individuals.<p>
So it touched many people. I didn't expect Mr. Imus to be fired, and we certainly didn't approach this with that idea. We really just wanted to have a face-to-face meeting with him. He needed to make a personal apology. And much to the players' credit, they wanted to see this man. They wanted to understand this man behind a mike, and how he could say such things.<p>
But we also wanted to say here's who we are. I happen to be the daughter of a coal miner. My father lost both his legs in a mine. He worked hard each and every day. He only stayed out of the mine six months until he got prosthetics.<p>
I know what it is to work hard. This has been a lifelong pursuit and passion--I've coached for 36 years. I haven't won a championship yet. I've taken different teams four times to a Final Four. But this was a special moment because it was the most unlikely of teams. It was a great story to be told.<p>
I was so very proud of these young women. And I can honestly say to you that I would gladly exchange winning a national championship if we, as young ladies, could stand up and allow the country to somehow be empowered, and we start talking about moral decency.<p>

