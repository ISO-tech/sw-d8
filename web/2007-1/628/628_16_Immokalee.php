<font face="Times New Roman, Times, serif" size="5"><b>McDonald's caves to farmworkers</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 20, 2007 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>HELEN REDMOND reports on the Immokalee workers' victory over the world's largest fast-food chain.</b><p>

THE COALITION of Immokalee Workers (CIW) has won a major victory against the world's largest fast-food chain--McDonald's. The company agreed April 9 to pay a penny per pound more to farmworkers who pick the restaurant's tomatoes.<p>
Former President Jimmy Carter negotiated the high-profile deal, a big win for a labor movement in hard times.<p>
Under the terms of the agreement, a third party will verify that the additional penny per pound will go directly to migrant workers who harvest tomatoes for McDonald's. Immokalee workers and the company will also jointly develop a workplace code of conduct that suppliers must follow, which will add to monitoring of working conditions, with inspection and compliance checks.<p>
The agreement is similar to one reached in 2005 with Taco Bell. According to Greg Asbed, a staffer with the CIW, a worker who currently earns about $190 a week picking tomatoes will earn $325. That's about a 70 percent raise.<p>
Lucas Benitez, a founding member of the CIW and a farmworker, said in a press release, "Today, with McDonald's, we have taken another major step toward a world where we as farmworkers can enjoy a fair wage and humane working conditions in exchange for the hard and essential work we do every day. We are not there yet, but we are getting there."<p>
Building alliances with communities, churches, unions and student organizations was critical to the success of the campaign. The CIW worked with AFL-CIO President John Sweeny and rank-and-file members of unions affiliated with the AFL-CIO, as well as the student organizations Student/Farm Worker Alliance, United Students Against Sweatshops and the National Latino/a Law Association.<p>
"Mickey Ds," like Taco Bell, didn't accept the CIW's demands without a fight. Prior to the agreement, they refused to meet with Immokalee representatives and ignored the McDonald's Truth Tour and Campaign for Fair Food, as they crisscrossed the country.<p>
The campaign drew hundreds of activists who protested and picketed out in front of McDonald's restaurants to educate customers about the slave-like working conditions and poverty wages of farmworkers. Buses carried activists from around the U.S. to Chicago for what was to be a major protest at McDonald's headquarters in suburban Oak Brook, Ill., in April.<p>
Corporate execs had already gotten a taste of what protesters had in store for them as the truth campaign shifted into higher gear.<p>
At many of the CIW supporters' actions, a protester dressed as Ronald McDonald handed out informational leaflets outside McDonald's restaurants, detailing the miserable pay and conditions that Immokalee workers endure to pump up McDonald's profits.<p>
Not surprisingly, McDonald's wasn't "loving it." The company sponsored a study that showed farmworkers <i>weren't</i> living in abject poverty--and tomato pickers in Florida earned $14 an hour, with some making even more. The study was quickly discredited by former Labor Secretary Robert Reich and others.<p>
The agreement will only cost McDonald's an estimated $250,000 a year, a drop in the bucket for a corporation that generates billions in sales worldwide. So why did they fight the CIW? Because the super-sized McMonster of fast food didn't want to concede that workers should be paid a living wage--or that they have any rights in the workplace. Not one of McDonald's 12,000 restaurants in the United States is unionized.<p>
The CIW's campaign, however, showed that even the biggest, most anti-union companies can be forced to make concessions when they're confronted with workers who are mobilized, determined and organized, with a base of support from labor, students and the left. At a time when established unions are on the defensive, these lessons of the CIW struggle are all the more important.<p>
The scheduled protest at McDonald's headquarters last week turned into a celebration at the House of Blues in Chicago. Members of the coalition spoke to the crowd of 1,500 about the meaning of the victory, and Zack de la Rocha and Tom Morello of the band Rage Against the Machine performed.<p>
Next target: Burger King. "Now we are going to focus on the other corporations in the fast-food industry like Burger King and Subway," said Benitez. A spokesperson for Burger King said the company has "no plans to change its position" regarding the tomato workers, despite McDonald's decision.<p>
The next campaign is already gearing up.<p>

