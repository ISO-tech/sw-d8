<font face="Times New Roman, Times, serif" size="3">When Al-Arian refused to answer, the guard continued, "It doesn't matter where you're from. If I had my way, you wouldn't be in prison. I'd put a bullet in your head and get it done with. You're nothing but a piece of shit."<p>
When Al-Arian objected, the guard again replied, "I know enough about all you guys. You're all pieces of shit. You can go pray to the fuck that you pray to."<p>
Al-Arian says that the guard refused to give him his name. The lieutenant in charge who reportedly heard the exchange then took Al-Arian aside and squeezed his right arm and right leg--while he was wearing handcuffs and leg irons--to the point that he lost feeling in his limbs during the four-hour trip to Alexandria.<p>
Al-Arian was reportedly told by the lieutenant to "Shut the fuck up"--and had his cuffs tightened seven times during the trip. When he arrived in Alexandria, the lieutenant repeatedly pulled the chain around Al-Arian's waist in an attempt to trip him.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">THOUGH HIS original trial in Florida ended with a jury finding him not guilty or deadlocking on all counts, Al-Arian is still behind bars.<p>
Faced with the threat of a retrial, he last year agreed to plead guilty to a single count of aiding the nonviolent activities of the Palestinian Islamic Jihad--in exchange for a short additional sentence and voluntary deportation.<p>
Al-Arian should have been released April 13 to rejoin his wife and five children. But Assistant U.S. Attorney Gordon Kromberg instead had Al-Arian transferred to Virginia to force him to testify in another case, despite the plea agreement that specifically exempted Al-Arian from having to testify. When he refused to cooperate, Al-Arian was hit with an additional 18-month jail sentence for civil contempt--a sentence that may be extended if he continues to refuse to take the stand.<p>
In protest, he began a hunger strike in January to bring attention to his indefinite detention and the inhumane treatment he has suffered while in custody, including racist abuse from guards and unsanitary prison conditions.<p>
After 60 days on strike--during which he lost 55 pounds, more than a quarter of his body weight--Al-Arian's family persuaded him to end the strike, fearing that his life was in danger. Reports of the new round of abuse came as Al-Arian recovered from the ordeal.<p>
As the Mexican American Political Association said in a recent statement announcing that it has joined the American Muslim Alliance and others in calling for Attorney General Alberto Gonzales to resign, Al-Arian's case is a "classic example" of government persecution of Arab Americans and Muslims in the wake of September 11. It's time to let Sami Al-Arian go!<p>

