<font face="Times New Roman, Times, serif" size="5"><b>Guards aim more abuse at Al-Arian</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 20, 2007 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">INSTEAD OF walking out of jail earlier this month as scheduled, Dr. Sami Al-Arian endured more racist abuse and a death threat from his prison guards.<p>
Al-Arian is the Palestinian professor who has been languishing behind bars on trumped-up charges of aiding terrorism since 2003. Now there is word of new harassment.<p>
According to the Tampa Bay Coalition for Justice and Peace (TBCJP), for nearly three days last week, Al-Arian's whereabouts were unknown by his family--as he was transported from the Federal Medical Center in Butner, N.C., where he had been housed during a recent hunger strike, to the Alexandria Detention Center in Virginia.<p>
During the trip, Al-Arian was housed for two days at a temporary facility in Petersburg, Va. When he arrived, according to the TBCJP, Al-Arian had his undergarments confiscated, and was given dirty, torn ones to replace them. He was placed in an unheated cell with pools of freezing water on the floor, and given no extra clothing or blankets to keep warm. Guards also reportedly seized his legal documents--then "misplaced" them.<p>
After 30 hours like this, Al-Arian was taken at 4:30 a.m. to be transferred to the Alexandria Detention Center. While being strip-searched for the trip, a guard reportedly asked him, "Where are you from? Afghanistan?"<p>

