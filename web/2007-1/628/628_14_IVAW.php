<font face="Times New Roman, Times, serif" size="5"><b>"Our goal is to educate people about Iraq"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 20, 2007 | Page 14</font><p>

<font face="Times New Roman, Times, serif" size="3">PAUL ABERNATHY and HARVEY THARP are members of Iraq Veterans Against the War (IVAW). At the March 17 antiwar demonstration, they talked to ANDERSON BEAM and KEVIN PROSEN about the occupation of Iraq and the future of the antiwar soldiers' movement.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>CAN YOU talk about your experience in the military?</b><p>

<font face="Times New Roman, Times, serif" size="4"><b>Harvey: </b></font><font face="Times New Roman, Times, serif" size="3">I enlisted in the Air Force right out of high school. I was trained as an Arabic linguist and deployed to Saudi Arabia five times from 1995 to 1997, on short tours, usually 30 to 90 days. When I got out, I joined the reserves and went to Ohio State Law School, and then joined the Navy JAG Corps.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Paul: </b></font><font face="Times New Roman, Times, serif" size="3">I joined the Army in 1996. I was broke, and I thought it would be a good experience. It was a different world back then. I was one of those guys who was totally gung ho, but I was never comfortable with the Iraq war.<p>
I was with the 3rd Infantry, and our initial mission was to build up temporary structures meant to help transport tanks through the desert. We crossed into Iraq and went into Baghdad. We were there for about a month and basically were charged with pacifying the city.<p>
There wasn't much use for my initial assignment after we took Baghdad, so after that, I spent six months in Anbar province, doing munitions recovery--basically recovering arms left over from the old Iraqi army.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Harvey: </b></font><font face="Times New Roman, Times, serif" size="3">When Iraq came up, they needed people who could speak Arabic. I took the Arabic proficiency test, which was basically the same test I had taken six years previously. I put down all the same answers down and passed it.<p>
I was in Iraq from October 2003 to 2004. For six months, I was deployed as an Arabic linguist for the Coalition Provisional Authority (CPA). Then I was made a projects officer in Kirkuk and worked closely with the Iraqi people, since I had Arabic language skills.<p>
In Kirkuk, it was very tense starting from early on. Criminal gangs were on the streets, and there was lots of sectarian tension among the three main ethnic groups in the city--Sunni Arabs, Kurds and Turkomen. It always felt like a powder keg, especially since something like 40 percent of Iraq's oil is in or around Kirkuk. So control of Kirkuk really meant control of a large part of Iraq's oil supply.<p>
A lot of times, we would be given missions that would violate rules of force protection, like needing itineraries for trips off base checked off in advance--rules that require having at least one other soldier with you on a mission. We were going on missions with no radios, no maps. If I actually followed the rules of force protection, I wouldn't have been able to leave the base a lot of the time.<p>
After working as a projects officer, they wanted to transfer me to Signals Intelligence, which would involve more combat. After working so closely with the Iraqi people and getting to know them, I couldn't justify killing.<p>

<b>HOW DID you get involved with the antiwar movement?</b><p>

<font face="Times New Roman, Times, serif" size="4"><b>Harvey: </b></font><font face="Times New Roman, Times, serif" size="3">I was always against the war, but after I came back, it was clear that there were no weapons of mass destruction. The hardest thing for me was separating the politics--my antiwar sentiments--with feelings of personal cowardice. I didn't want to see myself, or be seen by others, as a coward.<p>
But it's clear that the reasons for the war were fabricated--there was no threat from Saddam. Now the occupation is part of the problem, not a part of the solution.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Paul: </b></font><font face="Times New Roman, Times, serif" size="3">I had never heard of Iraq Vets Against the War or Veterans for Peace until I went to an antiwar march in Washington, D.C., in September 2005. I was wearing basically what I'm wearing now--camouflage, like a soldier--and I had this image of being this one lone guy. But then I ran into a bunch of other people dressed the same way. I basically joined at that moment.<p>
IVAW would be much bigger if veterans knew how to get involved in the network. It's hard to get media coverage of IVAW events. The Appeal for Redress got some coverage, but it wasn't like, "Here's a movement to get involved in."<p>

<b>WHAT'S NEXT for IVAW and the anti-war movement?</b><p>

<font face="Times New Roman, Times, serif" size="4"><b>Paul: </b></font><font face="Times New Roman, Times, serif" size="3">Our main goal is to raise public awareness and educate people about the U.S. role in Iraq. The U.S. is not a stabilizing force in Iraq. We want to teach them about Iraqi society, about the Iraqi resistance. They are resisting the occupation.<p>
We're also trying to reach out to people in the service--to give them a place to come to if they're against the war.<p>
We're also lobbying Congress for an end to the occupation, but also for veterans' benefits. We have guys who have Post-Traumatic Stress Disorder (PTSD), and the government finds out about it and just wants to drag them up.<p>
Also, we want reparations for the Iraqi people. We destroyed their country, and we need to help rebuild it--but it must be rebuilt by Iraqis. We're demanding reparations, so it's not true when people tell us we're "abandoning the Iraqi people."<p>

