<font face="Times New Roman, Times, serif" size="5"><b>A get-out-of-jail-free card for a terrorist</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 18, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">WHEN IS a terrorist not a terrorist? When he's a former ally of, and operative for, the U.S. government.<p>
Last week, a U.S. district judge dismissed immigration charges against right-wing Cuban exile Luis Posada Carriles, a man notorious for his suspected role in planning the October 1976 midair bombing of a Cuban commercial airliner that killed 73 people. Posada has also admitted to at least one hotel bombing in Havana in the 1990s, and is known to have been on the CIA payroll for much of the 1960s and early 1970s.<p>
In 2005, after being pardoned in Panama for his part in a plot to assassinate Fidel Castro--a pardon that was granted as a favor to the Bush administration--Posada snuck into the U.S. illegally and was eventually arrested on immigration charges.<p>
U.S. courts refused requests by both Cuba and Venezuela for his extradition, claiming that Posada likely would be tortured if sent to either country--an outrageous assertion coming from the country that ran the prisons at Abu Ghraib, Bagram and Guant&aacute;namo Bay.<p>
Posada's trial on immigration charges was scheduled to begin in mid-May, but instead, Judge Kathleen Cardone last week dismissed the charges and set Posada free, saying that the government had resorted to "fraud, deceit and trickery" in gathering its evidence against him.<p>
Among other things, despite the fact that Posada speaks English and had a lawyer present, Cardone contended that the lack of proper translation at Posada's initial interview after his arrest was improper and led to a "misunderstanding."<p>
Neither the courts nor the Bush administration has spared the same care for the rights of the more than 221,000 undocumented immigrants deported by Immigrations and Customs Enforcement (ICE) last year.<p>
"Fraud, deceit and trickery"? ICE routinely rounds up and deports undocumented immigrants "inadvertently" caught up under false pretenses in raids supposedly targeted at "violent criminals."<p>
As for "misunderstandings" and lack of proper translation, what about the March raid on a textile factory in New Bedford, Mass., where more than 360 undocumented workers--mainly Guatemalan women, many with small children--were rounded up and, when they couldn't produce proper documentation on the spot, almost immediately transferred to detentions centers, some in other states.<p>
The real crime is that a terrorist like Posada is allowed to walk free--while undocumented immigrants live in constant fear of deportation and separation from their families.<p>

