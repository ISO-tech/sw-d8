<font face="Times New Roman, Times, serif" size="5"><b>Daley's responsibility for Chicago police torture</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 18, 2007 | Page 13</font><p>

<font face="Times New Roman, Times, serif" size="3">IN THE wake of two politically embarrassing videotaped beatings of barroom patrons by Chicago police officers, Mayor Richard Daley is posturing as an advocate for police reform.<p>
After the pummeling of Karolina Obrycka, a 115-pound bartender, by Officer Anthony Abbate, and the beating of four businessmen by other Chicago officers, Daley accepted the resignation of Police Superintendent Phil Cline.<p>
Daley proposed that the Office of Professional Standards (OPS), the body that is supposed to investigate wrongdoing by police officers, should report to him, rather than the police superintendent. "We must assure every Chicagoan that we are doing everything possible to prevent abuse by police," Daley said.<p>
What the mayor failed to mention is that aldermen already are introducing an ordinance to scrap OPS and create a new independent Civilian Review Agency with the power to investigate all complaints against police officers.<p>
That Daley's proposal is the weaker of the two should come as no surprise to those who have been following his symbiotic relationship with torturers on the Chicago police force.<p>
The OPS itself in 1990 confirmed that when he was state's attorney between 1980 and 1989, Daley's office prosecuted dozens of African American men who endured electroshock to the ears and genitals, suffocation with plastic typewriter bags, mock executions and severe beatings until they decided to "confess."<p>
The men were victims of former Area 2 and Area 3 police commander Jon Burge, who was finally fired in 1993 for the torture of Andrew Wilson. Special prosecutors who last year completed a four-year, $7 million investigation further implicated the torturers.<p>
Daley received documentation from police Superintendent Richard Brzceczek as early as 1982 that Wilson had been tortured, yet he did nothing, and his assistant state's attorneys continued to take confessions in Chicago police stations from men who had just been tortured. Twelve of these men were even sentenced to death.<p>
Meanwhile, Daley parlayed his "tough-on-crime" reputation into political gain, winning control of the mayor's office in 1989 and holding onto it ever since. Aside from Burge, not one Chicago police officer involved in torture has been dismissed, let alone prosecuted.<p>
In the midst of public questioning last summer of Daley's role in the torture scandal, he said he would "apologize to anyone" and "take responsibility" for what happened on his watch. If Daley is truly to take responsibility, however, he must take immediate action to right the wrongs in which he has had a hand.<p>
First, the City of Chicago must take a hard look at why it has continued to employ torturers on the police force, rather than calling for their prosecution.<p>
Second, it must pay out civil suits to all torture victims with outstanding lawsuits, including but not limited to Madison Hobley, Stanley Howard, Leroy Orange and Aaron Patterson, former prisoners who suffered for years on death row before receiving pardons based on innocence from former Gov. George Ryan.<p>
Third, the city must cease paying Burge's generous police pension and covering his exorbitant legal fees.<p>
Finally, Daley must publicly call on State's Attorney Dick Devine and Attorney General Lisa Madigan to grant new trials to prisoners with unresolved torture allegations.<p>
Unless the mayor takes these steps, his claims to stand for reform will continue to ring hollow, a cruel joke to the dozens of torture victims who continue to languish in Illinois' prisons.<br>
<b>Julien Ball</b>, Campaign to End the Death Penalty, Chicago<p>

