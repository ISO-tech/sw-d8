<a name="UMass"></a><font face="Times New Roman, Times, serif" size="4"><b>University of Massachusetts-Amherst</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Charles Peterson</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AMHERST, Mass.--A recent decision by the University of Massachusetts (UMass) administration to award an honorary degree to former White House Chief of Staff Andrew Card has sparked a mass protest movement on campus in recent weeks.<p>
Card was chief of staff under George W. Bush, and served in the White House Iraq group. He had a central role in selling the illegal war in Iraq to the American people, and, like many others from the Bush administration, is widely despised because of it.<p>
The decision to award Card an honorary degree was only revealed at the beginning of May, and the UMass community wasted no time in organizing to prevent it.<p>
On May 3, a meeting of the faculty senate hosted Steven Tocco, the chair of the UMass board of trustees, the body ultimately responsible for awarding honorary degrees. The meeting was packed with Card opponents demanding that the degree be rescinded.<p>
Many audience members questioned Tocco as to Card's qualifications for such an honor. Tocco's response was "Andy is a nice guy. I play golf with him." When it was argued that Card is a war criminal for engineering a war that has cost the lives of almost 1 million Iraqis, Tocco proclaimed that "Card was not a war criminal on 9/11."<p>
The following week, a rally of more than 300 students protested outside of Whitmore administration building demanding the degree be revoked. Elvis Mendez, former president of the Student Government Association, explained that "the university shares the same values as the Bush administration," in particular, hostility to democracy.<p>
The administration has ignored resolutions passed by the Graduate Employee Organization, the Graduate Student Senate and the Massachusetts Society of Professors (MSP) chapter. All cited Card's intellectual dishonesty, and stated that, by UMass' own standards for awarding honorary degrees, he was totally unsuitable.<p>
Stephanie Luce, MSP vice president, summed up much of the sentiment: "This is an issue of campus democracy. Staff, students and faculty are continuously kept out of the decision making process. Workers should have a say in conditions of work."<p>
Protesters gave the administration until May 15 to meet their demands. As <i>Socialist Worker </i>went to press, the administration had yet to comment. However, another protest was in the works, with organizers vowing to continue demonstrating until the administration recognizes the wishes of the UMass community.<p>
