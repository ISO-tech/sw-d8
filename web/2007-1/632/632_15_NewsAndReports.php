<p><a name="Luis"></a><font face="Times New Roman, Times, serif" size="4"><b>Justice for Father Luis Barrios</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Dawning Greeenstreet</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Father Luis Barrios stood trial last week for charges brought against him after a September 19 protest of George Bush's appearance at the UN.<p>
Father Barrios-- a well-known community activist in the fight for immigrant rights and the antiwar movement in New York--and 15 other people were arrested when a confrontation broke out between protesters and the New York Police Department (NYPD).<p>
In an act of civil disobedience, Barrios attempted to cross the police barricade at the UN protest in order to "serve" Bush or one of his advisors with the findings and verdict from the Bush War Crimes Commission, where Bush was found "guilty of war crimes and crimes against humanity."<p>
Father Barrios was physically forced to stop by a police detective. Four other police officers then forced Father Barrios to the ground. But Father Barrios was accused of assaulting a police officer, resulting in a chronic injury to her wrist and a miscarriage six weeks after the incident.<p>
A packed courtroom full of supporters, students and activists turned out for Father Barrios' three-day trial, during which the city and the NYPD attempted to make an example of him--but to no avail.<p>
According to a report by Clark Kissinger of the "UN16 Defense," in handing down its verdict of "not guilty" on the most serious charges, the jury also expressed their opposition to the war in Iraq and their mistrust in the credibility of the NYPD.<p>
Father Barrios was found not guilty of assault and resisting arrest. He was convicted of disorderly conduct, but sentenced just to time served and $95 in court costs. When this verdict was handed down the courtroom erupted in chants of "S&iacute;, se pudo! S&iacute;, se pudo!" (Yes, we did it!)<p>
