<p><a name="Antiwar"></a><font face="Times New Roman, Times, serif" size="4"><b>Antiwar veterans speak out</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Amirah Goldberg</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BOSTON--On May 11, more than 60 people gathered in the First Church in Jamaica Plain to hear Liam Madden, of Iraq Veterans Against the War (IVAW) and cofounder of the Appeal for Redress GI antiwar petition, and Nancy Lessin and Charley Richardson, of Military Families Speak Out (MFSO), speak as part of the Jamaica Plain Forum lecture series.<p>
Madden spoke about the importance of organizing meetings and protests in every community, even if they are small. "Changing consciousness does not happen overnight," he told the crowd. "Even small meetings like this can crack open something larger because there is so much anger under the surface."<p>
He described a recent rally where IVAW members and others were speaking out against war and military recruiting. Spontaneously, an active-duty member of the National Guard came on stage in full uniform and set his redeployment orders on fire. Madden also shared that an IVAW chapter had been recently founded in Boston, a significant development for the antiwar movement here.<p>
Richardson argued that the political winds are changing, and that we need to hold Hillary Clinton, Barack Obama and other politicians to their words now that they seek to be seen as "antiwar candidates."<p>
The speakers each emphasized that wars are ended by people, not politicians. Many attendees were interested future antiwar actions, organizing and events in the area, and the evening showed the potential for renewed grassroots antiwar organizing.<p>

