<font face="Times New Roman, Times, serif" size="5"><b>Hip-hop isn't the real problem</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 18, 2007 | Page 13</font><p>

<font face="Times New Roman, Times, serif" size="3">THANKS SO much for taking on the ridiculous, hypocritical argument that sexism and racism primarily come from hip-hop ("How hip-hop got blamed for Imus," April 27).<p>
Don Imus sounded like a complete idiot when he leveled his attack against hip-hop on Al Sharpton's radio show, especially given the role Sharpton has played pushing for an end to sexist lyrics and ending the sometimes violent beefs between rappers. It's incredible how not only racist, but uniformed this asshole is.<p>
Imus hit a nerve because there is a huge amount of sexism in mainstream rap. But many voices from within the left and among Black leaders and journalists have spoken out about this again and again, while still being principled in standing up against the scapegoating of hip-hop (and Blacks in general).<p>
Sharpton has most often been on the progressive side of the debate, but some of his recent actions have blurred what is the larger problem.<p>
For example, At the annual National Action Network convetion on April 20, Sharpton went on the offensive. He reversed plans to give an award to Def Jam CEO L.A. Reid.<p>
This at first generated a backlash, with labels canceling donations and pulling guests from the conference. But then, in a "have my cake and eat it, too" moment, rap mogul Russell Simmons, founder of Def Jam, issued a statement calling for the bleeping or deleting of three more words from public airplay (on top of the standard seven prohibited by the FCC): "bitch," "ho" and "nigger." Artists can record them, but radio stations shouldn't air them.<p>
Multi-millionaire bosses like Simmons are trying to balance between public outcry and a proven business model: reproducing redundant, sexist tracks by artist who are promoted according to their sexual or violent exploits. Simmons was quick to say that this is not "censorship," and he fully believes in freedom of speech.<p>
The idea that industry heads are trying to protect the artist is a joke: Besides taking massive profits from the work of rappers, they work hand in hand with mega-corporations like Clear Channel that play the same tracks dozens of times a day in pre-packaged segments.<p>
Artists have little choice under this model--no exploration or variation wanted, thank you very much. Even artists with massive star power (and experience on the other side of the desk) like Jay-Z complain that business has trumped music in the music business, and is limiting anything really new from emerging.<p>
Earlier this month, Sharpton escalated the attack, demanding labels adhere to "standards"--producing music without offensive lyrics. His new Campaign for Decency led a march to the headquarters of Sony, Warner Music Group and Universal Music Group, but he is also calling for the voiding of contracts of any artists who are involved in a crime, and to stop signing artists who have criminal records.<p>
It's a bizarre turn for Sharpton to say people with a record shouldn't be allowed to work, given the immense difference in how Blacks and whites are treated by the criminal justice system and media. Twelve years ago, he got it right when he defended hip-hop artists, asking Time Warner bosses, "What do you expect them to sing, 'Hello Dolly?'"<p>
Hip-hop does reflect some of the worst degradation in our society, but no amount of censoring is going to cover up the poverty and violence of daily life for most non-white Americans.<p>
If a mobilized campaign of listeners who want more progressive music win out, this won't be a bad thing. But it won't stop the Imuses of the world from attacking Black women. It wouldn't matter if every radio station played Aretha Franklin's "Respect" all day, so long as Black households are still the poorest in the U.S. (earning less than two-thirds of whites).<p>
When people demand equality, the habits of disrespecting ourselves and each other fall away. Change isn't going to come from the executives or the politicians or the radio, but Imus' early retirement is a nice reminder that change is coming.<br>
<b>Amy Muldoon</b>, New York City<p>

