<font face="Times New Roman, Times, serif" size="3">"Because the post office is a monopoly, and because magazines must use it, the postal rates always have been skewed to make it cheaper for smaller publications to get launched and to survive. The whole idea has been to use the postal rates to keep publishing as competitive and wide open as possible.<p>
"This bedrock principle was put in place by James Madison and Thomas Jefferson. They considered it mandatory to create the press system, the Fourth Estate necessary for self-government...It was postal policy that converted the free press clause in the First Amendment from an abstract principle into a living breathing reality for Americans."<p>
Although the campaign has brought together strange bedfellows--the conservative <i>Weekly Standard </i>also faces huge increases--the case for stopping the rates is simple. Theresa Stack, publisher of the <i>Nation</i>, estimates that the magazine's postage costs could rise to $500,000, on top of what is already one of their largest budget items.<p>
Increases like that will certainly force many smaller publications out of business entirely. That is a recipe for an even greater shrinking of independent and minority opinions and reporting. As the <i>Boston Globe </i>wrote in an editorial, headlined "Don't Stamp Out Brainy Mags," "Price protection has been crucial for small magazines, helping them to add politically and socially diverse voices to the public arena."<p>
The idea of Time Warner et al having even more dominance in the "marketplace of ideas" should be a frightening thought to any proponent of democracy. Already, the lies of the political establishment go virtually unopposed, with small publications like the one you are reading trying hard to get alternative voices heard.<p>
A final decision on the rates is due in July. If the media behemoths get there way on this, it will be another blow to the independence of the press.<p>

