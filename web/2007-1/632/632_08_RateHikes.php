<font face="Times New Roman, Times, serif" size="5"><b>A threat to independent publications</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 18, 2007 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>JULIE FAIN of Haymarket Books reports on how publishing giants are trying to manipulate a postal rate hike.</b><p>

THE POSTAL Rate Commission has announced what could be a crushing blow to small magazine publishers: mailing rate hikes of up to 30 percent.<p>
But the pain wouldn't be felt equally across the industry--the biggest publications would see the smallest increases. That's because the new rates were largely written by publishing giant Time Warner, which produces more than 100 magazines.<p>
Last year, the post office proposed new rates that would have been shared evenly across all sizes of magazines--in itself, a disproportionate burden to smaller ones. But the giant publishers, with their teams of lawyers, lobbyists and insiders, prevailed in the next round.<p>
Fortunately, independent publishers got to work denouncing the rate hikes and organizing other publishers to oppose them. The independent media group Free Press initiated a campaign to save small and independent publishers.<p>
Bob McChesney, the founder of Free Press, pointed out that reduced rates for periodicals has a strong historic and democratic tradition. "This...revolves around America's very first and arguably most visionary and progressive media policy: postal rates for periodicals," McChesney wrote.<p>

