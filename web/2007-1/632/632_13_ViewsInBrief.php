<a name="STRIVE"></a><font face="Times New Roman, Times, serif" size="4"><b>STRIVE doesn't go far enough</b></font><p>

<font face="Times New Roman, Times, serif" size="3">A DAY prior to hundreds of thousands of immigrants taking to the streets of the U.S. demanding full equality and a stop to the raids, Rep. Luis Guti&eacute;rrez (D-Ill.), coauthor of the Security Through Regularized Immigration and a Vibrant Economy (STRIVE) Act, spoke to an audience of immigrants rights activists and the media in a town hall meeting in New York with about 150 people in attendance.<p>
The meeting was focused on "comprehensive immigration reform" and questions that the immigrant advocates might have. Participants included members of the Service Employees International Union Local 32BJ, New York Civil Participatory Project, International Socialist Organization, Latin American Integration Center and the May 1st Coalition. The meeting was held, ironically, at LaGuardia Community College, which has the highest number of immigrant students in the nation.<p>
Guti&eacute;rrez said the "dialogue has started" between the immigrant rights movement and its "allies" in Congress. He argued that we cannot lose the opportunity of passing immigration reform now, and that his proposal was the best bill that takes into account the "securicrats"--those in Congress who argue for the militarization of the border--and the largely working-class immigrant community that wants full legalization.<p>
He also mentioned the horrific effects that the raids have had and the increasing death toll at the borders. In this light, Guti&eacute;rrez introduced the STRIVE Act and its main components.<p>
However, the contradictions--between an enforcement-heavy and business-friendly bill, and the goal of taking undocumented workers out of the shadows and giving them the same rights that citizens have--were on display.<p>
A restaurant worker spoke about his experience of being hurt on the job and having to take a sick leave which he was not paid for, and for which his boss threatened to fire him if he did not return to work. He was also skeptical about the prerequisite during the six-year waiting period with the STRIVE visa of having to remain employed and in good "moral" character.<p>
Others in the audience, including Kavitha Pawria of Desis Rising Up and Moving (DRUM), questioned Guti&eacute;rrez about the STRIVE Act's provision allowing the federal government to "deputize" local law enforcements.<p>
Others questioned whether undocumented workers could afford to pay fines and "touchback" to another country during the 90 days of the application process. Guti&eacute;rrez referred to the fines as cheaper than paying union dues within the lapse of six years, and to the ladder as a "vacation."<p>
Although the majority of the audience agreed that with the STRIVE Act, a realistic dialogue has begun between the immigrants rights movement and Congress, everyone was in agreement about demonstrating on May 1 in order to put an end to the raids, and to demand full legalization for the undocumented. <br>
<b>Alvaro Lopez</b>, New York City<p>
