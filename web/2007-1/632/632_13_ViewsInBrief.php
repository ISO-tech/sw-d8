<p><a name="Justice"></a><font face="Times New Roman, Times, serif" size="4"><b>Nothing just about "criminal justice"</b></font><p>

<font face="Times New Roman, Times, serif" size="3">KEITH ROSENTHAL'S "Behind the talk about Boston's 'crime wave'" (April 27) is certainly correct that "young people need jobs, well-funded education, health care and control over their lives, not demonization and police abuse."<p>
My work as a community college teacher in Western Massachusetts reminds me on an almost daily basis that capitalism gives Black and Latino youths demonization and police abuse precisely because providing them jobs, well-funded education, health care and control over their lives would lower profits for the bosses.<p>
One of the largest programs where I teach is criminal justice, and the push is on--backed by federal and state grants--to increase Black and Latino enrollments.<p>
Marx analyzed the logic of this administrative initiative in his Economic Manuscripts of 1861-63, writing that crime "takes a part of the redundant population off the labor market" while "the struggle against crime absorbs another part of this population." Who says the revolutionary ideas of Marx are irrelevant?<br>
<b>Mark Clinton</b>, Northampton, Mass.<p>
