<p><a name="Eritrea"></a><font face="Times New Roman, Times, serif" size="4"><b>An error regarding Eritrea</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I JUST finished reading "Have the tables turned on the U.S. in Somalia?" (April 27) by David Whitehouse on your Web site. I believe his analysis is quite balance and appears to have a good grasp of the situation in the region.<p>
However, unfortunately, David has made a comment that somewhat diminished the article and his authority on the subject. His comment that Eritrea was "once supported by the U.S. when the USSR was aligned with Ethiopia's Mengistu Haile Mariam" is incorrect. At no point did the U.S. support Eritrea.<p>
The fiercely independent and anti-imperialist Eritrean socialist rebel groups were more of a concern to U.S. policymakers than the USSR and its Marxist Ethiopian ally Mengistu Haile Mariam. I wish David Whitehouse had avoided this unfortunate error.<br>
<b>Sam</b>, from the Internet<p>

