<font face="Times New Roman, Times, serif" size="5"><b>Iraq veterans write against the war</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Mary Howland</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 18, 2007 | Page 11</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Iraq Veterans Against the War,<b> <i>Warrior Writers: Move, Shoot and Communicate,</i> </b>2007.</font><p>

<font face="Times New Roman, Times, serif" size="3">SOLDIERS AND veterans have been writing poetry and literature for ages as a means of dealing with horrific situations and haunting memories.<p>
English literature and poetry has seen the likes of Siegfried Sassoon and Wilfred Owen depict the horrors of trench warfare during the First World War, and more recently has seen the likes of Tim O'Brien, Yusef Komunyakaa and Bruce Weigl illustrate the absurdity and violence of the Vietnam War.<p>
Iraq Veterans Against the War (IVAW) members are following suit, and the chapbook <i>Warrior Writers: Move, Shoot and Communicate </i>is the first published result of Iraq and Afghanistan war veterans and soldiers writing to make sense of their experiences fighting in a war they disagree with.<p>
<i>Warrior Writers </i>was conceived out of a writers' workshop for IVAW members, led by activist and writer Lovella Calica, editor of the book. The workshop was aimed to provide a space for veterans to reflect and to express themselves and their experiences in a creative outlet and a safe environment.<p>
The results were astonishing, poignant, heartbreaking and brutal. The success of the workshop and of the book has inspired Calica and other IVAW members to host more workshops around the country.<p>
The book is made up of poems, letters and mini-memoirs ranging from remembering the inescapable sand of the desert, to remembering a fallen friend, to dealing with guilt and anger. This book shows the true side of this brutal occupation, it tells us of the anger and weight veterans have to carry for fighting in a war they are opposed to.<p>
As Garett Reppenhagen writes in "Duffel Bags," "when you see a soldier returning home with a duffel bag at a bus stop, think about what is inside the bag. It might be rolled clothing of browns and tans. Or, it could be dark secrets that he will never reveal to his family."<p>
The overwhelming sense is that this book is not as much for a wider audience as it is for the IVAW itself. It is a tool of healing and support, but in doing this, it opens the door to a wider audience of people who are also opposed to the occupation, to gain a more real sense of what the soldiers' experiences.<p>
Every contribution in the book hits the reader in a different way. Aaron Hughes' poem "Ahmed" tells only one story out of thousands of an innocent little boy made victim of war. Although it is a devastating poem, it has a sense of hope to it, as well as love and forgiveness.<p>
The book is filled with the desire for the occupation to end, for the warfare and barbaric destruction to cease. Mark Lachance's "War Makes Monsters of Us All" sums it up best, "It is hard to fit into a life where everyone around me has no understanding that I lived my life for a year in a place where every decision I made either killed someone, or saved someone. I hope that in time we as veterans will find a way to bring our experience from war into society so our children will know the truth: war makes monsters of us all."<p>
<i>Warrior Writers </i>gives a human face to the harshness of the military and the destruction of the occupation. It is an amazing tool to be used in organizing against the war, and giving a voice to the soldiers who are now fighting back--fighting for a world free of occupation and violence.<p>
To get a copy of <i>Warrior Writers, </i>contact Drew Cameron at <a href="mailto:drew@ivaw.org">drew@ivaw.org</a> or Lovella Calica at <a href="mailto:lovella@ivaw.org">lovella@ivaw.org</a>. Copies will also be available at <a href="http://www.socialismconference.org" target="_blank">Socialism 2007</a> in Chicago, June 14-17.<p>

