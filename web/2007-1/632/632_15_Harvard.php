<font face="Times New Roman, Times, serif" size="5"><b>Putting pressure on Harvard</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 18, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>KEITH ROSENTHAL reports on the struggle to win a union contract for campus security guards.</b><p>

CAMBRIDGE, Mass.--The dark side of Harvard University is being forced into the light in the midst of a several weeks-old campaign by the university security guards' union and student activists to win a living wage for campus guards.<p>
Service Employees International Union (SEIU) Local 615 is negotiating its first-ever contract on behalf of 265 security guards who voted to join the union in 2006. The guards want living wages, steady full-time schedules, night-shift differentials and full union recognition.<p>
Currently, the guards receive the lowest starting wages of all service workers on campus, and earn far less in starting pay than guards at nearby MIT and Boston University.<p>
"We get $12.68 an hour, while most service workers around here make between $15 and $15.50 an hour starting pay," said Misty McGowan, a security guard who is part of the bargaining committee. "Yet Harvard is still willing to keep up us that much under everyone else."<p>

