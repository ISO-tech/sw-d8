<font face="Times New Roman, Times, serif" size="3">Negotiations are between the union and AlliedBarton, a private security guard firm contracted by Harvard. But at the end of the day, Harvard is responsible for ensuring that all workers on its campus receive fair wages.<p>
In 2002, in the wake of a campus-wide campaign for a living wage for Harvard's unionized custodial staff, Harvard adopted a "Wage and Benefits Parity Policy" stipulating that subcontractors who do business with Harvard must meet wage standards in line with Harvard's other unionized workers.<p>
But in the case of the guards, Harvard has so far been resistant to play any role whatsoever in negotiations.<p>
In response, a group of student activists in the Harvard Student Labor Action Movement (SLAM) started organizing to force Harvard to live up to its promises. On May 3, 11 students began a hunger strike that would last nine days--two hunger strikers ended up hospitalized.<p>
Kyle Krahel, one of the hunger strikers, said in an interview that he was motivated by the "unacceptable situation" that guards face.<p>
"They can't feed their families, they can't afford medicine, they can't afford rent," Krahel said. "We tried the rallies, and we tried the protests, and we tried the petitions, and sit-ins and letters and everything you could possibly think of, and after all that, they still weren't listening. So now we decided this was the best way to escalate the strike...and we've gotten the administration's attention."<p>
Although some students and faculty (not to mention administrators) criticized the hunger strike as an extreme tactic, it nonetheless electrified the campus, drawing increased local and national attention to the struggle and giving confidence to the guards to continue their fight.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">THE CAMPUS overwhelmingly rallied around the hunger strikers and the guards. Dozens and sometimes hundreds of students gather in Harvard Yard for 1 p.m. and 9 p.m. rallies that have become a daily ritual.<p>
On May 8, 200 students marched on the Holyoke Center, which houses the administrative and labor relations offices at Harvard. The demonstrators shut down the building for over an hour, with campus police refusing to let anyone in or out.<p>
An online petition has garnered signatures of more than 1,200 Harvard undergraduates and 1,000 faculty, staff and community members. At least 31 campus organizations signed on to support the campaign, including the Black Student Association, the Progressive Jewish Alliance, the Harvard College Democrats, the Association of Black Harvard Women, the Graduate Students Council, the Society of Arab Students and others.<p>
At the end of April, the Undergraduate Council (UC) passed legislation in support of the security guards, and seven UC members, including the president, went on a one-day fast in solidarity with the 11 hunger strikers.<p>
Though other campus unions, like the 4,800-strong Harvard Union of Clerical and Technical Workers (HUCTW), have yet to formally endorse the struggle, rank-and-file support inside the unions has been high.<p>
"Although our leadership has mainly ignored the campaign, many clerical union members have participated in rallies, vigils and other public pressure," said Geoff Caren, a union representative in HUCTW. "Some members have even fasted in solidarity with the students."<p>
On May 11, Harvard finally agreed to meet with the hunger strikers and issued a formal "concession statement" affirming its responsibility to intervene in negotiations and see that AlliedBarton meet wage parity standards.<p>
Following this announcement--and at the urging of the guards themselves, who were concerned about their health--the students ended their hunger strike at a 100-strong rally in the Harvard Yard, which took place despite a steady drizzle.<p>
Though the hunger strike is over, students and guards recognize that the struggle isn't won yet. "We have won this concession statement from Harvard, but we can't rest until we win a fair contract," said Kaveri Rajaram, another hunger striker. "We feel like the hunger strike was effective, but it's clear that we have to escalate further."<p>
Among other events, SEIU Local 615 is organizing a protest on May 17 at 2 p.m. at Holyoke Center.<p>
"If Harvard gives in to the union's demands, it will have local as well as national significance," McGowan said. "The way I feel is that our particular group is setting an example once we get a fair contract with Harvard," said McGowan. "As we speak, there are people trying to unionize and winning fair contracts all across the country. The movement is spreading."<p>

