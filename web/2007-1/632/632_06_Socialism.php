<font face="Times New Roman, Times, serif" size="3">Antiwar veterans and soldiers' rights activists will speak out in several other panel discussions. A panel discussion on resistance by soldiers today will feature Iraq Veterans Against the War members <b>Camilo Mej&iacute;a, Kelly Dougherty </b>and <b>Garett Reppenhagen. </b>Plus, <b>Todd Ensign </b>of Citizen Soldier and <i>International Socialist Review</i> contributor <b>Joe Allen</b> will speak on "The Vietnam Soldiers' Rebellion."<p>
Other independent journalists will have their say at Socialism, including <i>CounterPunch </i>co-editor <b>Jeffrey St. Clair, </b>speaking on "Hot Climate, Cold Cash: Making a Killing from Global Warming"; <i>Democracy Now!</i> radio host <b>Amy Goodman </b>on "Static: Government Liars, Media Cheerleaders and the People Who Fight Back"; Air America's <b>Laura Flanders </b>on "Blue Grit: True Democrats Take Back Politics from the Politicians"; and <b>Joshua Frank </b>on the story behind Democratic Party presidential hopeful Barack Obama.<p>
Radical writers and activists from around the world will attend, including <b>Luciana Genro </b>from the Party of Socialism and Liberty of Brazil, and <b>Charles-Andr&eacute; Udry </b>from the editorial board of <i>&Agrave; L'Encontre</i>, Switzerland.<p>
Activists from the immigrant rights movement will also be on hand to discuss next steps in the fight to stop the raids and deportations. So, too, will activists against the death penalty and the criminal justice system, including <b>Barbara Becnel, </b>advocate of Stan Tookie Williams, who was executed in 2005; and <b>Yusef Salaam, </b>who was exonerated in the 1989 Central Park jogger case in 2002.<p>
Several meetings will draw out the lessons of past struggles, such as the Black Power movement--<b>John Carlos, </b>the 1968 Olympic medalist will appear with <i>Nation </i>sportswriter and author <b>Dave Zirin </b>and former NFL player and author <b>Anthony Prior </b>on the panel "From 1968 to Rutgers: Athletes Fight for Social Justice." The inspiring lessons of the Flint sit-down strike of 1937 will be taken up by <b>Sharon Smith</b>, author of <i>Subterranean Fire</i>.<p>
Many speakers will emphasize the importance of Marxist politics and building socialist organization today. <b>Paul D'Amato, </b>author of the book <i>Meaning of Marxism, </i>will speak on the 90th anniversary of the Russian Revolution, and <b>Anthony Arnove, </b>author of <i>Iraq: Logic of Withdrawal</i>, will speak on Marx's <i>Communist Manifesto</i> and its importance today.<p>
And that's only a small part of the meetings taking place at Socialism 2007. Don't miss it!<p>

