<font face="Times New Roman, Times, serif" size="5"><b>Socialism for the 21st century</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 18, 2007 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">"SOCIALISM FOR the 21st Century" is the theme of this year's <a href="http://www.socialismconference.org" target="_blank">Socialism conference</a>--which will draw together activists and socialists from around the U.S. and the world to more than 100 workshops and panel discussions aimed at rebuilding the left in the U.S.<p>
Last year, Socialism--which is cosponsored each summer by the Center for Economic Research and Social Change and <i>Socialist Worker's </i>publisher, the International Socialist Organization--turned out some 1,500 people.<p>
Featured among the speakers at this year's event are activists from the frontlines of organizing against the war in Iraq. Renowned journalist, activist and documentary filmmaker <b>John Pilger </b>will screen his film <i>Breaking the Silence: Truth and Lies in the War on Terror</i>.<p>
<b>Jeremy Scahill</b>, independent journalist and author of the bestselling expos&eacute; <i>Blackwater: The Rise of the World's Most Powerful Mercenary Army, </i>will tell the ugly story of war profiteering run amok in Iraq.<p>
As an unembedded independent journalist with firsthand knowledge of the war and occupation's impact to the people of Iraq, <b>Dahr Jamail </b>will speak on "Beyond the Green Zone"--and <b>Dahlia Wasfi</b>, an Iraqi-American physician who has traveled to Iraq, will describe the day-to-day effects of occupation.<p>

