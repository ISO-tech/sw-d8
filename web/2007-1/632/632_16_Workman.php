<font face="Times New Roman, Times, serif" size="5"><b>Was an innocent man executed in Tennessee?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Julien Ball</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 18, 2007 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">THE STATE of Tennessee has executed a man that people around the country and the world believe was innocent.<p>
On May 9, Tennessee authorities put to death Philip Workman for the 1981 killing of police Lt. Ronald Oliver during a botched armed robbery at a Wendy's restaurant in Memphis.<p>
Workman's execution was the first in Tennessee since the state halted executions for 90 days due to its flawed lethal injection protocol that called for shaving an inmate's head and having a fire extinguisher on hand--preparations needed when the state still used the electric chair to execute prisoners.<p>
Workman's conviction was the result of shoddy forensics, police and prosecutorial misconduct, and the testimony of a single eyewitness. But because of the inaction of Workman's incompetent defense attorneys and the difficulty of getting a court to consider evidence of innocence that comes to light after a trial, no judge or jury ever considered the facts that proved his innocence.<p>
For example, the story of Harold David, the supposed eyewitness to the murder, was never examined. Nobody ever saw Davis at the scene of the crime--not the five people nearby, nor officers Aubrey Stoddard and William Parker, who were involved in the incident that led to Oliver's death.<p>
Davis' own sister, Jacqueline Davis-Moden, said her brother supported his drug habit by serving as a professional witness, scanning the news for details of crimes and then collecting reward money by providing the testimony he believed police were looking for. And Davis' friend Vivian Porter claimed she was with Davis the night of Oliver's death, and that they hadn't witnessed the crime.<p>
In 1999, Davis gave Workman's attorneys two videotaped statements recanting his testimony, stating that he hadn't seen the shooting. He said police "basically told me what happened...I said I didn't see all that. They said, well, this is what you're going to say."<p>
Davis said he tried to back out at trial. "I kept telling the prosecutor I really don't feel good about this," he said. "Late one night, a big white guy came and knocked on my door. He said he had a message for me and that, if I changed my testimony in any kind of way, people I love and care about could disappear, just like I could."<p>
The physical evidence also shows that Workman didn't fire the gun that killed Oliver.<p>
Steve Craig, another witness at the scene, never testified, but he said in a sworn affidavit in 1996 that another officer, William Parker, exchanged shots with Workman. The Memphis Police Department used bullets that were consistent with the fatal wound suffered by Oliver. Several ballistics experts have presented evidence showing that the .45 caliber pistol Workman was carrying couldn't have caused such a wound.<p>
In other words, the evidence in the case points to the shooting of Oliver as an instance of "friendly fire" by Officer Parker, not murder by Philip Workman.<p>
But Workman's defense attorneys during his original trial explored none of these issues. They told Workman that his guilt was a foregone conclusion, and told the jury that its decision would be whether to convict him of "murder in the first degree or murder in the second degree."<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">WHEN WORKMAN'S final appeal was denied, the Tennessee Department of Corrections (DOC) denied his request for a last meal. Workman had wanted a vegetarian pizza donated to an area homeless shelter, but DOC officials refused.<p>
When news of the DOC's coldness emerged, scores of regular people ordered pizzas to be delivered to homeless shelters in Workman's honor--from Minnesota to Texas, and across Tennessee.<p>
This outpouring of humanity in support of Philip Workman stands in contrast to the callousness of the Tennessee legal system over a 26-year period. It shows that ordinary people care about death row prisoners and points to the possibility--and the need--to build a movement that puts a human face on the death penalty.<p>

