<font face="Times New Roman, Times, serif" size="5"><b>Mumia's legal case reaches key stage</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 18, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">PHILADELPHIA--Supporters of political prisoner Mumia Abu-Jamal are mobilizing around what could be a major break in the fight to free the outspoken opponent of the criminal justice system after 25 long years on Pennsylvania's death row.<p>
On May 17, the Third U.S. Circuit Court of Appeals will hear arguments by Mumia's lawyers. Good news came for Mumia's attorneys when the court announced that it would allot an additional hour to oral arguments, giving the NAACP Legal Defense Fund and National Lawyers Guild attorneys the time to present material in their friend-of-the-court briefs.<p>
The court will then decide whether to grant a new trial, affirm Mumia's life sentence or reinstate the death sentence.<p>
The court will consider four issues--including the question of whether prosecutors excluded African Americans from Mumia's jury by using peremptory challenges.<p>
At Mumia's trial, the prosecutor used 11 of his 15 peremptory challenges to remove Black jurors. The trial jury was composed of 10 whites and two Blacks, even though Philadelphia is 44 percent Black.<p>

