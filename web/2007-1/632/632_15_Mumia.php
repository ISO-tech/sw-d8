<font face="Times New Roman, Times, serif" size="3">Also at issue is whether Mumia was denied his right to due process and equal protection under the law due to extreme racism and bias on the part of Judge Albert Sabo, who commented that he was "going to help 'em fry the n----r."<p>
The court also announced recently that it rejected a motion by state prosecutors to recuse, or remove, all the judges for the appeals court. The motion was a failed maneuver on the part of prosecutors to move Mumia's trial from a court that has leaned toward liberal decisions in the past into a more conservative one.<p>
Mumia was framed and convicted in 1982 for the shooting of Philadelphia police officer Daniel Faulker. His trial proceedings were riddled with racism, prosecutorial and judicial misconduct, the suppression of evidence and coerced witness testimony.<p>
Before his arrest, Mumia was an independent journalist dedicated to exposing the brutality and racism of the Philadelphia police and courts. From behind bars, Mumia has continued to write, becoming known by death penalty and criminal justice activists as the "Voice of the Voiceless."<p>
Activists are turning up the heat at this critical time in Mumia's case. "Mumia can still be executed," said Pam Africa, spokesperson for International Concerned Family and Friends of Mumia Abu Jamal. "Further, since the Supreme Court is unlikely to hear Mumia's case, this is realistically his last chance to get a new trial. As the history of his case shows, we need public pressure to ensure the court's fairness."<p>
Supporters bought a full-page ad to appear in the Nation calling for justice for Mumia. Endorsers include Harry Belafonte, Angela Davis, Alice Walker, Danny Glover, Howard Zinn, Lynne Stewart, Michael Ratner and many others.<p>
On May 17, anti-death penalty activists will mobilize for protests in Philadelphia and San Francisco to put pressure on the Third Court to do the right thing in Mumia's case.<p>

