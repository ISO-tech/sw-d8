<font face="Times New Roman, Times, serif" size="5"><b>Sleazy investments of the Gates charity</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Leela Yellesetty</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 19, 2007 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">A RECENT expos&eacute; by the <i>Los Angeles Times </i>has revealed that the Bill and Melinda Gates Foundation has investments in companies that contradict and even directly undermine its stated goals.<p>
The Gates Foundation is the largest charitable organization in the world, with total assets of nearly $66 billion--more than the gross domestic product of 70 percent of the world's nations. The Gateses--along with their latest contributor Warren Buffet--have been lauded by the media for fighting the AIDS epidemic, hunger, homelessness and other pressing social problems.<p>
Like other charities, the Gates Foundation gives away 5 percent of its net worth each year--that's enough to avoid paying any taxes. The remaining 95 percent is invested. Investment decisions are made to reap maximum profits, with no reference to the foundation's social mission.<p>
According to the <i>Times'</i> detailed analysis, 41 percent of the foundation's assets, totaling $8.7 billion, are invested in companies that conflict with the foundation's mission.<p>
Examples of irresponsible investing include some of the world's worst polluters, including ConocoPhillips, Dow Chemical and Tyco International; predatory lending companies such as Sallie Mae; fraudulent health care firms; and chocolate companies profiting from the slave labor of children.<p>
Even the foundation's one restriction on its investments--no tobacco companies--has been violated. At least $43 million in holdings are directly tied to tobacco profits.<p>
Perhaps most glaring is the foundation's heavy investment in pharmaceutical companies, whose monopoly on AIDS medications have put life-saving drugs out of reach for millions of poor Africans--the very people the Gates Foundation claims to be helping.<p>
As the <i>Times </i>expose points out, "Some critics say the foundation's failure...might trace back to the source of most of its money--Microsoft--at which Bill Gates serves as chairman. "Microsoft monopolies in computer operating systems and business software depend upon the same intellectual-property and trade-law approaches favored by drug companies."<p>
The <i>Times </i>feature offers poignant examples of individuals who have been helped by the Gates Foundation--while simultaneously being harmed by the companies it invests in.<p>
One example is a 14-month-old child in Nigeria who got vaccinations provided by the Gates Foundation, but suffers from the heavy pollution of oil companies that the foundation invests in. Another is an elderly Seattle couple who received housing assistance from a Gates-supported non-profit--after losing their home due to the fraudulent practices of Ameritrade, in which the Gates Foundation invests.<p>
The Gates Foundation claims it will reassess its investment practices. But this won't fix the underlying hypocrisy of corporations that make their millions exploiting ordinary people--and then expect applause for handing us back a few crumbs.<p>

