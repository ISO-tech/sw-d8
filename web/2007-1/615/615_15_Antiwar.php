<font face="Times New Roman, Times, serif" size="3">The national mobilization in Washington, along with regional mobilizations in other cities, is a critical opportunity to take the antiwar sentiment expressed by voters in the November elections and give it a concrete expression in the streets.<p>
The Win Without War coalition issued the initial call for the January 11 pickets "to mobilize citizen opposition to the war and put pressure on Congress to stop it," but many groups took up the call and brought their own political messages to the events.<p>
-- In <b>Chicago, </b>about 250 people from peace groups and antiwar coalitions came out, and a large number said they also planned on attending the January 27 protest in Washington. People sang, and many signed an Out Now petition spearheaded by antiwar author and activist Anthony Arnove, Noam Chomsky and Howard Zinn.<p>
-- In <b>Ithaca, N.Y., </b>about 70 people came out to a rally organized by a few individuals in 24 hours. The crowd gathered at a busy intersection with handmade signs calling for the troops to leave now.<p>
-- In <b>Queens, N.Y., </b>more than 25 activists took to the street in front of the military recruitment center. This was the third such protest this month, which enjoyed enthusiastic support from passersby in this predominantly immigrant community. More than 350 flyers were handed out for the January 27 demonstration, and several people signed up for the buses leaving from Queens.<p>
-- In <b>Eugene, Ore., </b>about 100 people braved a snowstorm to gather in the Wayne Morse Free Speech Plaza downtown. Protesters stood on the sidewalk with antiwar banners, as passing drivers honked their horns to show their agreement.<p>
-- At the University of Washington campus in <b>Seattle, </b>30 people rallied and marched in the street to a nearby marine recruiters' office and picketed there for half an hour before marching over a nearby freeway. About 40 people in downtown Seattle handed out leaflets to passersby who were largely supportive.<p>
<i>Aaron Amaral, Evan Kornfeld, Steve Leigh, Graham Shaw and Rebekah Ward contributed to this report</i>.<p>

