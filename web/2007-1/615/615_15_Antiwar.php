<font face="Times New Roman, Times, serif" size="4">Antiwar organizers get ready for a day of action</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Building for January 27</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 19, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">SEEKING TO build on growing antiwar sentiment, organizers across the U.S. are gearing up for a national day of action on January 27. Large mobilizations are planned for Washington, D.C., San Francisco, Seattle, San Diego, Austin, Texas, and several other cities.<p>
The potential for a big turnout was on display after Bush gave his January 10 speech outlining his plan to send more than 20,000 additional troops to Iraq. The next day, hundreds of antiwar pickets were held across the U.S. to express opposition to the plan.<p>
Just weeks ago, plans for the Washington, D.C., demonstration on January 27 were still up in the air, but the reaction among antiwar activists to Bush's speech appears to have brought more urgency to the organizing.<p>
Also in the past several weeks, the mobilizations have taken clearer shape in San Francisco and elsewhere, in some cases with new organizations taking the necessary initiative to pull together the forces for these protests.<p>

