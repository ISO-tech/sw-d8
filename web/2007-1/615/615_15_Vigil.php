<font face="Times New Roman, Times, serif" size="5"><b>Mother of NYPD shooting victim begins vigil</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Brian Jones and Jared Rodriguez</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 19, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Valerie Bell began a vigil outside of the 103rd Police Precinct in Queens on January 1 to protest the murder of her son at the hands of four undercover police officers.<p>
"As a mother, I will never hold my son again, and his father will never have a father-son conversation," she said. "It is the grace of God, who is our pillar of strength, that will allow us to get through this."<p>
Bell has vowed to spend one day outside of the precinct for every one of the 50 bullets that were fired at her son and two of his friends on November 25 as they left Sean's bachelor party on the morning of his wedding. All three men were unarmed. Sean Bell was killed, but his friends eventually recovered from their injuries.<p>
Since that morning, there have been dozens of protests around the city demanding justice for Sean Bell. But almost two months later, the Queens District Attorney has yet to indict a single officer involved in the shooting.<p>
The grand jury is expected to hear the case as <i>Socialist Worker </i>goes to press, and supporters of the Bell family are trying to increase the pressure for an indictment. Rev. Al Sharpton and Sean Bell's fianc&eacute;e, Nicole Paultre, met with New York Attorney General Andrew Cuomo and asked for a special prosecutor, but none has been granted.<p>
After the massive march in Manhattan on December 16, many felt the need for a next step, but none was put forward by the march organizers.<p>
Bell's vigil could become a lightning rod for those who want to continue the movement. Already she has been joined in her vigil by a constant stream of friends, relatives and other supporters.<p>
And on Martin Luther King Day, 100 people from several organizations converged there, including the NAACP, the Malcolm X Grassroots Project, the Dominican Women's Center and the ISO. Speakers read from King's antiwar speeches, and the crowd sang "We Shall Overcome."<p>
The failure to convict (or even to indict) police officers that kill sends a message that the lives of ordinary people--especially working-class Blacks and Latinos--are worthless. Everyone who thinks differently must support Valerie Bell's vigil--and be prepared to keep the pressure on the city to deliver justice for Sean Bell.<p>

