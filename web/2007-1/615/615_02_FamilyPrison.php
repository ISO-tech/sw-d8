<font face="Times New Roman, Times, serif" size="5"><b>Immigrant families behind bars</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 19, 2007 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>CINDY BERINGER reports on a Texas jail where immigrant families--men, women and children--are locked up.</b><p>

THE FOREBODING white multi-story structure--trimmed in concrete and tall, barbed-wire-topped fences--has undergone a name change. The former T. Don Hutto Correctional Center is now called the T. Don Hutto Residential Center.<p>
"You can call it what you want," said one person at a rally outside the building. "Just look at that ugly thing--it's a prison."<p>
A crowd of approximately 100 people gathered last month outside the privately owned detention center near the small town of Taylor, Texas. They were protesting the incarceration of immigrant families and children.<p>
When Jay Johnson-Castro learned of Texas' latest, but little-known, distinction in the annals of injustice, he decided to walk the 35 miles from Austin to Taylor to bring attention to the plight of these immigrant families. Johnson-Castro had previously gained media attention when he walked 200 miles from Laredo to Brownsville to protest plans to build a wall on the border.<p>
Texans United for Families, a coalition of community, civil and immigrant rights groups, organized the protest. After a press conference at the state Capitol building in Austin on December 13, several coalition members joined Johnson on his march. Other activists joined concerned community members along the way and at the site for a protest and vigil.<p>
According to the <i>Austin American-Statesman</i>, "The Taylor jail began holding immigrant families in the summer under a contract with the federal Immigration and Customs Enforcement Agency. It is owned and operated by Corrections Corporation of America."<p>
Texans United for Families reports approximately 400 immigrants are incarcerated in this hellish place, and at least half of the prisoners are children. Many of the immigrants--who are limited to places other than Mexico--have made requests for asylum in the U.S. For an indefinite period and without any charges, they await deportation hearings.<p>
According to Johnson-Castro, at least two families who came to the country with the proper documentation are in the jail, awaiting deporting.<p>
Immigration attorney John Gibson represents two Palestinian families, including a pregnant woman and five children ranging in age from 2 to 17. After the families were detained in early November, the fathers were separated, and the women and children sent to the Hutto jail. A 2-year-old was put in foster care. Gibson says that the Palestinians entered the country legally, but have been denied asylum.<p>
The prison flag of Corrections Corporation flies proudly next to the U.S. and Texas flags--as if locking up children were something to be proud of.<p>
What goes on behind the flags is horrific. Johnson-Castro says that the "prisoners" are kept in their cells for 22 hours a day. They receive an hour of English instruction and an hour of recreation, mostly indoors. The media are not allowed inside, he added, and attorneys are not allowed to observe the conditions. Children have had their education interrupted and little medical care is available. Everyone wears prison uniforms bearing their names and numbers.<p>
Adela Medina came from Houston to protest. She said she had been able to visit her niece who is inside, but had to speak to her on a telephone through a glass wall. Her niece's daughter was refusing to eat because the food was so bad.<p>
Attorney Frances Valdez said that "detainees have reported receiving substandard medical care, and becoming ill from food served at the jail."<p>
Valdez believes that the detainees suffer from several psychological issues. "Most of these people are asylum seekers, so they've already suffered severe trauma in their country." But the psychological problems are not treated.<p>
The Taylor jail and a smaller one in Pennsylvania are the only facilities in the U.S. that lock up immigrant families and their children on non-criminal charges. But Homeland Security Secretary Michael Chertoff wants to open more jails for families.<p>
As University of Texas student and organizer Luissana Santibanez told the crowd at the protest, "The prison system is out of control in this country. We need to start a campaign to shut this thing down."<p>

