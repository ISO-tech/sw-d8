<p><a name="Ariz"></a><font face="Times New Roman, Times, serif" size="4"><b>Fight Arizona's anti-immigrant Prop 300</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jeff Bale</b></font><p>

<font face="Times New Roman, Times, serif" size="3">PHOENIX--Every January, tens of thousands of college football fans flock here for a frenzy of bowl games. This year, they were greeted by hundreds of college students and their supporters, chanting "We are students, not criminals!"<p>
They were protesting a new statewide anti-immigrant measure passed in the elections last November. The measure, Proposition 300, would deny undocumented immigrant students the ability to pay in-state tuition at public colleges and universities, even if they are longtime residents of the state.<p>
At major schools like Arizona State University (ASU), this draconian measure translates into a prohibitive jump in tuition--from around $5,000 per year for full-time undergraduates to almost $18,000. This will force any number of students to drop out of school.<p>
"Our parents can't afford $18,000 a year," Ari, an 18-year-old freshman at ASU, told reporters. "I would have to drop out."<p>
Miguel, a junior at ASU and a U.S. citizen who served four years in the Navy, also marched in protest of the measure. "I'm here because this does not just affect the undocumented, it affects the entire community," he said.<p>
The tragic irony of Prop 300 is that it targets some of the state's neediest students who, despite the odds, have achieved success in Arizona's criminally underfunded school system. Many of the students affected by Prop 300 came to Arizona as young children and have earned the grades that have allowed them to continue their education.<p>
The protesters also expressed support for the DREAM act, a bill before Congress that would make it easier for undocumented students with high-school diplomas to attain legal status more easily.<p>

