<a name="Minutemen"></a><font face="Times New Roman, Times, serif" size="4"><b>No the Minutemen</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Orlando Sep&uacute;lveda</b></font><p>

<font face="Times New Roman, Times, serif" size="3">REPUBLICAN POLITICIANS and far-right groups staged a series of political events January 13 to express their support for two Border Patrol agents convicted of shooting Mexican national Osbaldo Aldrete-D&aacute;vila as he fled on February 17, 2006, near Fabens, Texas.<p>
The action began with a Capitol Hill press conference January 10, when Rep. Dana Rohrabacher of California called on President Bush to keep "our heroes" out of jail. Accompanied by Reps. Duncan Hunter (Calif.), Ted Poe (Texas), Walter Jones (N.C.), Joe Wilson (S.C.) and Tom Tancredo (Colo.), Rohrabacher vowed to "find out whose side you're on...the American people or the side of our enemies."<p>
The Minuteman Project and affiliated organizations followed up with Free the Border Patrol rallies in various cities, including Chicago, Los Angeles, San Diego and Phoenix in support of agents Ignacio Ramos and Jos&eacute; Alonso Compean, both due to report to jail on January 17 to serve sentences of 11 and 12 years, respectively.<p>
The Minutemen protests came just one day after a Border Patrol agent shot and killed an undocumented immigrant, Javier Dom&iacute;nguez Rivera, in Cochise County, Ariz.<p>
Aldrete-D&aacute;vila's case has become an easy mark for the far right because his truck carried more than 700 pounds of marijuana. But according to U.S. Attorney Johnny Sutton, neither agent knew the truck contained the drugs. In fact, the Office of the Inspector General of the Homeland Security Department acknowledged that the Border Patrol agents "were out to shoot Mexicans."<p>
U.S. Attorney Sutton stated that "the defendants were prosecuted because they had fired their weapons at a man who had attempted to surrender by holding his open hands in the air, at which time Agent Compean attempted to hit the man with the butt of Compean's shotgun, causing the man to run in fear."<p>
In a press conference Sutton declared "being a United States Border Patrol agent is not a license to shoot people. It is especially not a license to shoot unarmed...suspects who are running away from you...It is not a license to write a report and turn it in which leaves out the fact that you shot an unarmed suspect who was running away from you."<p>
The Minutemen are using the fact that Ramos and Compean are both of Mexican descent to try to hide their racism. But at their Chicago rally, a Minutemen placard made fun of Aldrete-D&aacute;vila's wound in his gluteus and expressed regret that he hadn't been shot in the head.<p>
The anti-immigrants "suggested" to Latino counter-demonstrators that they go bus their tables. Answering taunts about where had they left their hoods--a reference to the Ku Klux Klan--the racists responded, "We don't need them...yet."<p>
-- At <b>Los Angeles' </b>Federal Building, 40 immigrant rights activists confronted 125 on the other side. A pro-immigrant activist using a bullhorn was arrested when a police officer decided to enforce an anti-noise ordinance while ignoring the use of bullhorns by the Minutemen.<p>
-- In <b>Chicago, </b>40 immigrant rights activists confronted 50 bigots--where in the past, counterprotesters have outnumbered the racists by 10-1. Activists concluded that the fact that the bigots didn't come to an immigrant neighborhood or to a Mexican Consulate diminished the sense of urgency. Also, part of the movement, particularly in Chicago, seems to be prioritizing political lobbying.<p>
Nonetheless, the activists vowed to keep organizing and building rapid response networks to defend immigrants in case of similar events organized by racists as well as immigration raids and deportations.<p>
<i>For information about this rapid response network, send an email to <a href="mailto:I_Defend_Immigrants@yahoogroups.com">I_Defend_Immigrants@yahoogroups.com</a>. Sarah Knopp contributed to this report</i>.<p>
