<font face="Times New Roman, Times, serif" size="5"><b>Tensions build in Cochabamba</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 19, 2007 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>SARAH HINES reports from Bolivia on new confrontations between left- and right-wing forces.</b><p>

AFTER WEEKS of escalating tensions, clashes in Bolivia between supporters and opponents of Cochabamba's ultra-right-wing Gov. Manfred Reyes Villa left at least two dead and more than 200 injured last week in the city of Cochabamba.<p>
The confrontation was precipitated by the Reyes' call for a new referendum on the question of regional "autonomy," despite the fact that 63 percent of the state's resisdents rejected the proposal last June.<p>
"Autonom&iacute;a" is the demand of the oligarchy in the gas- and oil-rich eastern departments known as the <i>Media Luna</i> (Half Moon). If achieved, it would give these departments a larger share of oil and gas revenues.<p>
Reyes jumped on the autonomy bandwagon in December in a move that has helped propel him to the lead of the right-wing opposition to President Evo Morales, the country's first indigenous president and leader of the Movement Towards Socialism party.<p>
Protests were first called by Cochabamba's Regional Workers Center (COD), but a majority of the protesters are indigenous peasants and <i>cocaleros</i> (coca growers) from the department's rural provinces, who converged in Cochabamba over the course of more than a week of protests and confrontations that turned bloody last week.<p>
According to eyewitness accounts, the violence began when members of Youth for Democracy--which, despite its misleading name, is a semi-fascist group of wealthy youths with ties to the governor--rushed through police lines to attack <i>cocaleros</i> and indigenous women with two-by-fours, lead pipes and baseball bats.<p>
The right-wingers screamed racist insults--one protester reported hearing, "You are not from Cochabamba, go back to your <i>pueblo</i> <i>indios de mierda</i>." By the end of the night, at least one <i>cocalero</i> and one Manfred supporter were dead.<p>
The Morales government has refused to support the social movements' demand for Manfred's resignation, instead urging the movements to allow him to finish his term. Morales' response to the recent violence was to call for "dialogue" and tell protesters to lift their blockade of the city and go home.<p>
Nevertheless, the following day, Morales felt compelled to introduce legislation that would allow for referendums to recall a governor, mayor or president. Reyes announced he would withdraw his proposal for a new referendum on regional autonomy.<p>
While these results show that the protests had some effect, the struggle is far from over. As Claudia, a Cochabamba resident and participant in the protests, said in an interview, "We almost died in this struggle, and still the government refuses to support our demands."<p>
There are two very important dynamics at play in this struggle that will have an impact on the future direction of Bolivian politics. One is the battle of the oligarchy against the government and the left. The other concerns the relationship between the social movements and the government, which very clearly tried to demobilize the movement against Manfred Reyes.<p>
Despite the government's call for the end of protests and blockades, as <i>Socialist Worker</i> went to press, leaders of the COD and indigenous, <i>cocalero</i> and peasant organizations were vowing to continue their protests until Manfred resigns, and several roads into the city remained blockaded.<p>
It will take further mobilization to defend the Morales government from increasingly organized and aggressive right-wing forces--and to achieve the social movements' demands for a restructuring of Bolivian society in the interests of workers, peasants and the poor.<p>

