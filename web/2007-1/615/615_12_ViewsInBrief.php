<p><a name="Unity"></a><font face="Times New Roman, Times, serif" size="4"><b>Is Iraqi unity impossible?</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I REALLY enjoyed the interview with Nir Rosen ("This is a U.S. crime," December 8, 2006). He does such a great job explaining the evolution of this ugly business gripping Iraq today.<p>
It is remarkable how he boldly pins the blame on the occupation, saying that the divide-and-rule method we have seen there has created the conditions from which the civil war has grown.<p>
It is difficult to challenge someone with as much firsthand knowledge as Rosen has, but I hope he is wrong when he says that things have gone so far that there is no possibility of a united Sunni and Shiite resistance. This also seems to be the conclusion Ashley Smith draws in his recent article ("Bitter fruit of occupation," December 15).<p>
I can understand that it's probably not likely that someone would want to fight side by side with the same people who just blew up a car bomb in their neighborhood, but it seems to me that under the right circumstances, that might not have to matter too much.<p>
There's a lot of talk about a "surge" and "cleaning up Baghdad"--in other words, attacking the Mahdi Army led by Moktada al-Sadr, recently named the biggest threat in Iraq by the Pentagon. Maybe this is why it is taking Bush so long to make up his mind about the Iraq strategy.<p>
The U.S. ruling class knows that it has to start extracting itself from Iraq in some visible way, but they also realize that if they do they will be leaving behind a very powerful, anti-U.S. cleric in charge of half of the country, and Baathists and jihadists in other big chunks. Will they give it "one last hurrah?" Indications are yes; they just assassinated the Sadrist leader in Najaf, and have been conducting provocative raids in Baghdad.<p>
It's questionable whether the U.S. could even defeat the Mahdi Army in a large-scale conflict. It might end up looking like what Hezbollah dealt Israel this summer. A new offensive could also be unpopular at home, with a public that just voted essentially to draw the war to a close. It might even mean the collapse of the Iraqi puppet government.<p>
But we should never underestimate the arrogance of our ruling class--and besides, they probably don't have much of a choice.<p>
If Sunnis and Shiites were being attacked simultaneously, they could resist simultaneously, and the civil war operations could fall more into the background. Sadr would do his usual maneuver and appeal to all patriotic Iraqis. Even shaky, strategic agreements could be a conceivable beginning to a future unity.<p>
It can be a tough thing for opponents of this wretched war to see a resistance so crippled by reactionary politics and falling for the divisiveness of the occupation. But the resistance is what it is. Their motivations may sometimes not be things that make socialists very happy, but they must be seen as people struggling to find a way to end their continual victimhood at the hands of outside powers.<p>
The Iraqis have been thrown into a maelstrom and have had to form a resistance under the most unfavorable conditions. But for all of their failings, we have to recognize that they are apparently doing enough to tie up U.S. imperialism.<p>
What our government has done in Iraq is disgusting, but we should not despair. The Iraqi resistance and the war weariness of the American population have created the possibility to deliver a huge setback to the empire--an opportunity we need to seize on.<br>
<b>Chuck Stemke</b>, San Diego<p>
