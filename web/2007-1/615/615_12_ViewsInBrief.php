<p><a name="Oaxaca"></a><font face="Times New Roman, Times, serif" size="4"><b>Real analysis of Oaxaca</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I MUST comment on Avery Wear's response ("The stakes in Oaxaca," January 5) to Todd Chretien's excellent letter ("What's next in Oaxaca," December 8) on the struggle in Oaxaca. Calling what happened there a "temporary, small" socialist revolution is extremely misleading.<p>
When Lenin commented that in every strike is the hydra of social revolution, he did not consider every strike a social revolution. We know that in Russia the soviets (workers' councils) sprang up out of elected strike committees. Nevertheless, that should not lead us to declare that every strike committee is a soviet.<p>
Nor should we necessarily consider the formation of a popular assembly to guide a popular struggle a "social revolution"--big, medium or small.<p>
As revolutionaries, we hold an important responsibility of analyzing and learning from struggle, not simply cheerleading, but learning. The struggles in Bolivia, Venezuela and Argentina have gone far further (so far) than the struggles in Mexico, yet none of these have been social revolutions--that is, have involved the overturning of capitalist social relations and their replacement by workers' power.<p>
In Argentina, the popular assemblies were more widespread. Yet they were never able to involve the big battalions of the working class and never developed beyond local organs of struggle and debate.<p>
Real analysis is what we need, not superficial observation. Such analysis would have to take into account such factors as the degree to which the people of Oaxaca actually see the assembly as an alternative government and the extent to which the assembly is representative of major workplaces and communities in the region.<p>
Even from a distance, it is possible to see that the Popular Assembly of the Peoples of Oaxaca is not in a position to pose itself as the Oaxacan "commune," let alone challenge for state power nationally.<p>
Such an analysis, moreover, would have to take into account the fact that, first, the teachers have gone back to work; and second, the movement has stalled without achieving its main demand, the removal of Oaxaca's PRI governor from power.<p>
In Argentina, on the other hand, the movement succeeded in replacing several presidents--though it was never able to pose itself as a political alternative. And in Paris and Petrograd, workers actually held power!<br>
<b>Paul D'Amato</b>, Chicago<p>

