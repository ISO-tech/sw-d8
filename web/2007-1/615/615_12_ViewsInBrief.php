<a name="Voice"></a><font face="Times New Roman, Times, serif" size="4"><b>A voice for prisoners</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IF IT weren't for <i>Socialist Worker</i> newspaper, I wouldn't have learned about or heard about the brothers' situation and struggle on Texas' death row ("Suffering on Texas' death row," November 17).<p>
I guess to the civilized world, inhumane conditions for death row prisoners is not news or their problem. I guess starving yourself to bring awareness to your cause isn't news that the people of Texas want to hear. So thanks, <i>SW,</i> for giving a voice to those of us who have been made mute.<br>
<b>Ronnie Kitchen</b>, Menard, Ill.<p>
