<font face="Times New Roman, Times, serif" size="5"><b>Will Big Brother be opening your mail?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 19, 2007 | Pages 1 and 2</font><p>

<font face="Times New Roman, Times, serif" size="3">GEORGE BUSH is giving the federal government the right to search your mail without a warrant.<p>
On December 20, during a congressional recess, Bush attached a presidential "signing statement"--which gives the president the right to modify, "interpret" or even nullify portions of a law--to the Postal Accountability and Enhancement Act of 2006.<p>
Originally, the law contained a provision prohibiting the opening of first-class mail without a warrant. But in the signing statement, Bush said the law's restrictions should be relaxed. "The executive branch shall construe [the law]...in a manner consistent, to the <i>maximum extent permissible</i>, with the need to conduct searches in exigent circumstances, such as to protect human life and safety against hazardous materials, and the need for physical searches specifically authorized by law for foreign intelligence collection," the statement reads.<p>
In other words, Bush has cleared the way for the government to go snooping.<p>
"The President's signing statement raises serious concerns that the administration's warrantless surveillance of telephone calls and Internet communications extends to the U.S. mail, as well," ACLU Executive Director Anthony Romero said in a statement. "Given the President's dismal record of violating the privacy rights of Americans, we must question whether he is authorizing the opening of mail without a warrant in violation of the Constitution and laws enacted by Congress."<p>
The White House claims that there's nothing new in the signing statement, and that postal regulations already allowed inspectors to open mail without a warrant. But that applies in only very narrow circumstances--when there is credible evidence that a package contains a bomb or other immediately dangerous materials.<p>
In the new statement, Bush specifically widens the scope of allowable searches in what civil liberties groups say is an intentionally vague way--raising the specter of a return to the U.S. government's mail surveillance programs of the 1950s, '60s and '70s, which targeted communists and other radicals.<p>
The new signing statement does not, for example, describe what "circumstances" are necessary before a search can be conducted, leading activists to question if mail from individuals on government watch lists--known to be highly inaccurate--could be opened.<p>
"I don't think the White House would have included this language into a signing statement unless the feds were either already searching mail without a warrant or planning to do so," CBS News legal analyst Andrew Cohen said. "And if the legal right to do so were as clear as the White House now says it's hard to believe that there was a need to remind everyone of the fact in a bill about the postal service."<p>
Spying is nothing new for the Bush administration. In late 2005, it was revealed that the Bush administration had authorized the National Security Agency to eavesdrop on phone conversations and view the e-mail messages possibly thousands of people inside the U.S.--without a warrant.<p>
But as one an anonymous senior U.S. official told the <i>New York Post</i>, "You have to be concerned" by the Bush administration's latest move. "It takes Executive Branch authority beyond anything we've ever known."<p>

