<font face="Times New Roman, Times, serif" size="3">In a blatant attack on free speech, lead prosecutor Capt. Daniel Kuecker has subpoenaed reporters Sarah Olson, Dahr Jamail and Gregg Kakesako and antiwar activists Phan Nguyen and Gerri Haynes to testify against Watada. Outrageously, they face six months in prison and a $500 fine for contempt of a military court if they don't testify.<p>
"Initially, Captain Kuecker attempted to ask me certain questions, which I didn't answer, about who was involved in the organizing of the initial press conference and how involved was Lt. Watada in it," said Nguyen in an interview. "It is alarming as an activist that I might be expected to have to testify to the military how activists might be organizing against the military."<p>
Despite the military's attempt to intimidate supporters, the campaign to defend Watada is in full force.<p>
Speakers at the January 4 rally included Sara Rich, the mother of Suzanne Swift, a soldier who refused to return to Iraq after enduring sexual assault by her commanders, and Darrell Anderson, the first Iraq veteran to publicly flee to Canada. A handful of Iraq Veterans Against the War (IVAW) members who have been on a speaking tour of the U.S. set up "Camp Resistance" just outside the gates of Ft. Lewis.<p>
On June 21 and 22, there will be a citizen's hearing in Tacoma, Wash., on the legality of U.S. actions in Iraq. Modeled after similar tribunals during the Vietnam War, its aim is to put the war, not Watada, on trial.<p>
After the January 27 protest in Seattle, Watada will take part in a panel with Aaron Dixon, former Black Panther and Green Party U.S. Senate candidate, and Chanan Suarez-Diaz, president of the Seattle chapter of the IVAW. And the campaign has a full day of protest planned for the February 5 court martial.<p>
Building mass support to defend war resisters like Watada will be key to reinvigorating the antiwar movement.<p>

