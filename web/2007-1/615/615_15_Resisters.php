<font face="Times New Roman, Times, serif" size="5"><b>Put the war on trial, not the war resisters</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Darrin Hoop</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 19, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--More than 100 people rallied January 4 outside the gates of Ft. Lewis near Seattle in support of Army First Lt. Ehren Watada.<p>
As the first commissioned officer to publicly refuse to take part in the occupation of Iraq, Watada faces a court martial that begins on February 5. The rally took place at the time of his pre-trial hearing.<p>
Watada faces two years in a military prison for "missing movement" for his refusal to deploy with the First Stryker Brigade last June. He faces four additional years, one year per count of "conduct unbecoming an officer and a gentleman," resulting from antiwar statements he made to the press.<p>

