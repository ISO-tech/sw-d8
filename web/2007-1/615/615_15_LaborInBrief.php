<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">January 19, 2007 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3"><B>STORIES BELOW:</B><BR>
<A HREF="#Legal">Legal Aid Society</A><P>

<a name="Legal"></a><font face="Times New Roman, Times, serif" size="4"><b>Legal Aid Society</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Lucy Herschel, delegate, Legal Aid Society Chapter of 1199SEIU</b></font><p>

<font face="Times New Roman, Times, serif" size="3"><br>NEW YORK--1199SEIU members at the Legal Aid Society have seen layoffs, concessions and the de-unionization of certain departments. Now, union officials and management have collaborated to push through a two-year contract with only one 3 percent raise.<p>
1199SEIU represents the support staff, paralegals and social workers at the Legal Aid Society, the biggest provider of legal services to New York's poor and working class.<p>
Members organized to vote down management's 3 percent contract offer in November, mainly because the cost of living in New York City has gone up more than 8 percent over the last two years. Members were also angry that the raise was not fully retroactive.<p>
The contract was defeated by a 60-40 percent vote--the third time in three years that Legal Aid members have voted against the recommendation of their union leadership.<p>
However, the union leadership, including a rank-and-file negotiating committee, called for a re-vote on the same contract a month later on December 14. This time, Legal Aid management was invited to the union's re-vote meeting to make its case.<p>
"It was inappropriate," Carlie Gordon, of the Queens Legal Aid office, told <i>Socialist Worker.</i> "If the contract was voted down once, why are re-voting on the same exact offer again? Is that even allowed? We were basically forced to accept this contract."<p>
Members had organized to prevent the re-vote, collecting more than 100 signatures on a last-minute petition and gathering with signs and flyers outside the union hall on the day of the vote. Nevertheless, the contract passed the second time around.<p>
With another contract deadline in 10 months, some members are calling for a change in the chapter. "1199 has a fighting history, but the officers we work with are not fighting for us," word processor Abida Chaudhry told <i>Socialist Worker.</i> "They don't encourage the membership to fight for a better contract, and they don't back us up when we do fight. Instead, they use scare tactics to keep us from fighting."<p>
Lauthie Reyes, a support staff member at one of the Manhattan offices, said, "We need to show management that we are not scared, that we won't back down and that we are serious about our demands. If not, our voice won't be heard and management will continue to walk all over us. Everyone needs to be together to show the strength of our union."<p>

