<font face="Times New Roman, Times, serif" size="5"><b>The debates ahead for the World Social Forum</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 19, 2007 | Page 10</font><p>

<font face="Times New Roman, Times, serif" size="3">DENNIS BRUTUS is a world-renowned South African poet and anti-apartheid campaigner who was imprisoned with Nelson Mandela in the 1960s. In the 1990s, he became a leading voice in the global justice movement and the World Social Forum (WSF). This year's WSF takes place in Kenya this month. Dennis wrote to <i>Socialist Worker </i>to offer his perspective on the upcoming conference.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">THE WORLD Social Forum starting in Nairobi, Kenya, January 20 looks to be a major event that will have an impact on African and global affairs. As many as 100,000 are expected; there will certainly be more than 50,000.<p>
The usual problems are expected, as they were at the previous World Social Forums in Porto Alegre, Brazil, and Mumbai, India, and the smaller-scale polycentric forums last year in Bamako, Mali; Caracas, Venezuela; and Karachi, Pakistan. Some of these are probably unavoidable--arranging places to stay, transportation, inadequate technology, etc.<p>

