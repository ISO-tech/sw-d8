<font face="Times New Roman, Times, serif" size="3">Still, this is likely to be an event of global significance and an important component in the worldwide movement to try to create a better world; building on the Zapatista slogan, Another World is Possible.<p>
Many of the discussions and panels--about 1,000 of them over five days--will focus on globalization--sometimes, more broadly, imperialism, or American imperialism. Others will center on corporate globalization, with a focus on more specific aspects of the globalization process.<p>
Crucial--and more controversial--debates will focus on the nature of the Forum itself and on its future. Will it continue to be--as its critics call it--a talk shop? Or, as some insist, is it time for it to become a body that also takes decisions and engages in action?<p>
Other burning issues that need to be discussed are U.S military action in Africa, Africa's increasing importance in oil wars, and the troubling complicity of African governments in U.S. action in Africa. Another urgent topic is the AIDS pandemic that is devastating Africa--with a serious failure to act in South Africa and elsewhere in the continent.<p>
In any case, bringing the message of "Another World is Possible" to Africa will be of significance to the continent and the world. The world will never be the same again.<p>

