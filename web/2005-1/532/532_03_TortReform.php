<font face="Times New Roman, Times, serif" size="5"><b>Their wish is Washington's command</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 25, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THE BUSH administration skated to another easy victory this month when Congress approved the "Class Action Fairness Act."<p>
There's nothing "fair" about this bill for ordinary people. It's another brazen giveaway to Corporate America.<p>
Under the new law, class-action lawsuits seeking more than $5 million in damages will move to backlogged federal courts if fewer than a third of the plaintiffs are from the same state as the primary defendant. Sound innocent enough? In reality, this law will make it nearly impossible to file a coordinated, national class-action lawsuit to hold corporations responsible for their negligence--or worse.<p>
The Bush administration says that the legislation was needed to curb "junk" lawsuits filed by ambulance-chasing lawyers trying to bankrupt honest companies.<p>
Trial lawyers are a favorite target for Republicans--especially since they tend to be reliable supporters of the Democratic Party.<p>
But plenty of Democrats were willing to jump on the bandwagon, too. The bill passed Congress by a wide margin, with 50 House Democrats and 18 Senate Democrats voting in favor. Liberal darling Sen. Barack Obama (D-Ill.) was among them. The behavior of the "party of working people" offered further evidence that the Democrats will do pretty much anything to prove themselves to their corporate masters.<p>
Now, analysts are predicting that the ease with which the Bush administration pushed through tort reform could serve as a model for other attacks. According to the <i>Nation's</i> Dan Zegart, "The GOP is hoping the almost total collapse of the Democrats on the Senate bill--eight Democrats co-sponsored it--means improved chances of passing a bill curbing asbestos suits and a reworked medical malpractice measure that caps damages for pain and suffering and drastically limits suits over dangerous drugs like Vioxx."<p>
Then, there's the bankruptcy "reform" bill written by the credit card companies that would take away the last recourse of people buried in debt--and leave them in hock to bankers for the rest of their lives.<p>
The Bush administration is gearing up for its biggest attack on ordinary people with the looming fight over its plan to privatize Social Security.<p>
Recently, media pundits have been predicting that the White House faces too much opposition from Democrats and moderate Republicans to get away its plan. Don't count on it. As with tort "reform," the Democrats' opposition has been pitiful--accepting the need for some kind of reform to make Social Security function better as an "entrepreneurial" program.<p>
Bush's major domestic policy initiatives--starting with his massive tax giveaway to the rich in 2001, and continuing on to his plans for Social Security privatization--have all been unpopular with large numbers of ordinary people. But the Democrats' failure to make a stand has meant that the Bush team got away with their schemes anyway.<p>
Building a real opposition to Bush means recognizing that we can't depend on the politicians in Washington--whatever their party.<p>

