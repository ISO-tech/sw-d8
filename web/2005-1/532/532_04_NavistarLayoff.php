<font face="Times New Roman, Times, serif" size="5"><b>How much can we take?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 25, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">I AM a laid-off employee from Navistar, United Auto Workers Local 402. I am sure you are aware of the laid-off employees from this plant. But here is a real kick in the butt: Some employees like me were on disability for the last year.<p>
Unfortunately, we had no idea Navistar was going to be having such drastic layoffs. Surgery is something you can't prevent. If the doctor says you have no other choice, then it has to be done.<p>
In June, when I had surgery, I had just been through the death of my mother and three sisters from cancer. I filled out my papers properly and was told that I did have some credited weeks from a job that I was fired from (because of attendance issues due to death and sickness in my family). I was sent a letter saying that I had to draw those weeks first and then, at end of those weeks, I would be eligible for Trade Readjustment Assistance (TRA). <p>
My unemployment ended in November. Now I am being told that you had to have worked 26 weeks on the job or been on workman's comp to qualify for TRA. That was not brought up at any time. It was never in any information sent to me. This is unacceptable. After all, President Bush said he would train us. <p>
The letter about the TRA had given me hope, after having lost so much--my car, getting bad credit, delinquent house notes. Now my house is slated for foreclosure on February 25. <p>
After seven months of being told that I was TRA-eligible, now there are qualifications about whether I was considered on workman's comp or disability. What's the difference? Both are authorized by a legitimate physician who said you are not able to work. It is against our rights to pick out clauses to disqualify workers for training after snatching their jobs from under them and sending jobs to other cities and countries.<p>
Our government is messed up. This is the third job I have worked that has been outsourced. The government has allowed U.S citizens like me and others to sink into debt and lose their homes, cars and credit. <p>
How much more can we take? <p>
Frigidaire closed down in 1979 and moved to a Texas General Motors plant. American Carco Plating company did parts for Delco. It moved to Mexico after I had eight years on the job. Now, Navistar International.<br>
<b>Anonymous, </b>from the Internet<p>

