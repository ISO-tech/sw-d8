<font face="Times New Roman, Times, serif" size="4">Bush selects Negroponte as Washington's new super-spy</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Dirty wars and diplomacy</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 25, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THE BUSH administration is signaling a new era of covert operations and dirty wars.<p>
Just before George Bush left to "mend fences" with European leaders--sorting out their common interests in pressuring Syria and Iran, and their disagreements over Iraq--he sent a different message by appointing John Negroponte to the newly created job of Director of National Intelligence. Negroponte, who is currently the U.S. ambassador to Iraq, will take charge of the U.S. government's sprawling array of spy agencies, from the CIA to the armed forces to the National Security Agency.<p>
From Bush's perspective, Negroponte is the ideal man for the job. He's a veteran of official Washington who can hold his own in turf battles with the likes of Defense Secretary Donald Rumsfeld. More importantly, he helped preside over secret U.S. wars in Central America in the 1980s--not to mention Washington's ironfisted imperial rule in Iraq during the last eight months.<p>
To this day, Negroponte claims to be ignorant about the death squads that operated in Honduras when he was ambassador there in the early 1980s--as it was literally turned into a U.S. aircraft carrier to pressure the left-wing Sandinista government in neighboring Nicaragua and the revolutionary movement in El Salvador.<p>
The tiny country became the eighth-largest recipient of U.S. foreign aid. At the same time, Honduran security and military personnel were "disappearing" their opponents--a recent government report documented nearly 200 such cases, though there are allegations of many more. "Time and again...Negroponte was confronted with evidence that a Honduran army intelligence unit, trained by the CIA, was stalking, kidnapping, torturing and killing suspected subversives," the <i>Baltimore Sun </i>reported in a 1995 investigative series.<p>
It was in Honduras that Negroponte perfected the skills so highly prized by the Bush administration--the ability to lie about the U.S. government's murderous policies in the face of overwhelming evidence, and to maintain the fa&ccedil;ade of democracy of a U.S. client state while using it as an instrument of imperial policy.<p>
Negroponte's record in Honduras won him the post of U.S. ambassador to the United Nations in 2001. Senate Democrats who had earlier squawked about his record shut their mouths after the September 11 attacks, and voted to confirm him three days later.<p>
As ambassador to Iraq beginning in 2004, Negroponte used the skills he honed in Honduras on a much bigger scale, organizing an election that left the U.S. with the upper hand--while U.S. troops flattened Falluja, with tactics similar to those used by Russian troops in Chechnya.<p>
Some in the Washington establishment have criticized Negroponte for his lack of experience in the intelligence services. This misses the point: U.S. spying isn't about gathering information for its own sake, but manipulation and intervention to further the interests of the U.S. empire.<p>
Which is why he's an ideal man for the super-spy job--as Washington, while playing nice with Europe, is methodically preparing for a new round of interventions.<p>
This could include a military strike on Syria or Iran, or a new coup attempt in Venezuela, among other possibilities. At a forum in Washington, D.C., Scott Ritter--the former weapons inspector in Iraq who became a harsh critic of the Bush administration--said that Bush has signed off on orders for an air strike on Iran planned for June of this year.<p>
For their part, European governments will resist or complain about this or that aspect of U.S. policy. But their objections reflect imperial rivalries, not principled opposition to great-power politics. That's why France rushed troops to Haiti following the U.S.-sponsored coup in that country in 2004--and why Paris and Washington are now working together to push Syria out of Lebanon, using the assassination of Lebanese Prime Minister Rafik Hariri as a pretext.<p>
For the U.S.--as for every imperial power in history--invasions and negotiations aren't opposites. On the contrary, dirty wars and diplomacy are two sides of the same coin--different means to achieve the single end of imperialist domination. <p>

