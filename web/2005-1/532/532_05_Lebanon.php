<font face="Times New Roman, Times, serif" size="5"><b>How the "great powers" used Lebanon as a pawn</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 25, 2005 | Page 5</font><p>

<font face="Times New Roman, Times, serif" size="3">WITHIN HOURS of the mysterious assassination of former Lebanese Prime Minister Rafik Hariri, U.S. mouthpieces were demanding that Syria withdraw its forces from Lebanon, cease meddling in Lebanese affairs and end its support for Hezbollah guerrillas.<p>
But a look at the history of Syria's intervention in Lebanon shows that the U.S. and Israel tacitly--if not openly--supported the Syrian occupation for nearly three decades.<p>
It wasn't always like this for the imperial overlords of the Middle East, both European and American. For centuries, Lebanon was considered a district of the larger area of Syria, under the Ottoman Empire.<p>
At the end of the First World War, when Britain and France divided up the Ottoman Empire in the Middle East, the French took Lebanon and Syria. In order to forestall the development of an Arab nationalist movement that would assert Syria's independence from France, France created Lebanon in 1920, relying on a Maronite Christian elite as its chief source of support.<p>
"The 'State of the Greater Lebanon' proclaimed by the French General Gourand on August 31, 1920 was...a totally artificial, French-created entity," wrote Robert Fisk in his book on Lebanon, <i>Pity the Nation</i>. "Its frontiers, over 20 years later, would become the borders of the independent Lebanese state. It was in defense of the presumed 'sovereignty' of this peculiar nation--a product of the Quai d'Orsay, rather than the creation of any Arab national aspiration--that countless thousands were to die more than half a century later."<p>
When the French and British colonial empires crumbled after the Second World War and the U.S. moved in to take their places, Washington became the main guarantor of Lebanese independence, as a bulwark against Arab nationalism. The U.S. underscored this commitment when it sent 20,000 Marines to prop up the right-wing government of Camille Chamoun, when it faced a nationalist challenge inspired by the 1958 revolution in neighboring Iraq.<p>
For the U.S., Lebanon represented a pro-Western state ruled by a religious minority whose identity insulated it from the appeal of Arab nationalism--not unlike Israel.<p>
But the two decades after Israel's founding pushed thousands of Palestinian refugees into Lebanon. The demographic and political balance on which a pro-Western Lebanese government had rested was shifting. To make a long story short, the result of these shifts was the 1975 Lebanese civil war--between the forces of the right, allied with Israel and grouped around the Christian Falange; and the left, involving Arab nationalists, Palestinian and Druze formations, and others. <p>
Originally, Syria backed the left in the civil war. But in 1976, with the left on the verge of capturing power, Syria invaded Lebanon on the side of the right. The Syrian regime concluded that having a right-wing government allied with Israel in power in Lebanon was preferable to having a Lebanon controlled by leftist militias.<p>
Not coincidentally, the U.S. agreed. It acknowledged the "positive role that the Syrian government play[ed] in Lebanon."<p>
Fifteen years after Syrian troops moved into Lebanon, the U.S. again blessed their role. In a successful bid to win Syrian support for the 1991 war against Iraq, the U.S. gave its tacit approval to a Syrian takeover of the Lebanese government.<p>
In the years since, the Syrians have worn out whatever welcome they once had. Today, almost all major political forces in Lebanon--including those that found themselves on opposite sides of the 1975 civil war--are calling for Syria to get out. To this, the U.S. and France have attached their own demands for withdrawal of Syrian troops.<p>
With anti-Syrian demonstrations numbering in the hundreds of thousands, the U.S. probably has visions of pulling off the type of "democratic regime change" in Lebanon that it recently won in Ukraine. The U.S. reasons that if it can get a stable, pro-Western government in Lebanon without Syria's help, that's better than having to rely on Syria to guarantee it for them. And in Washington's eyes, any defeat to Syria is a defeat to Syria's ally Iran--and a victory for the U.S.'s main ally, Israel.<p>
In all of these machinations the biggest losers are the ordinary people of Lebanon and Syria--about whom the U.S. cares not at all.<p>

