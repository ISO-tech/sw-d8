<font face="Times New Roman, Times, serif" size="5"><b>How the owners destroyed hockey</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Dave Zirin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 25, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">THERE'S NOTHING left but the autopsy, and it doesn't take <i>CSI's</i> William Peterson to decipher who killed the National Hockey League (NHL).<p>
The season is canceled. The accusations are flying. But the most deafening sound is silence. "It's not a good sign when your replacement programming is outperforming the NHL," said one ESPN executive. The sport--in short--is a corpse.<p>
The media--as per usual in a sports/labor conflict--has chosen to heap blame on both sides, saying this is a case of "greedy billionaires vs. greedy millionaires."<p>
I stand with the players in union fights. I would link arms with them even if their demand was for the owners to be body-checked by Scott Stevens at center ice before every home game. All demands are reasonable because it's their sweat that keeps the puck moving.<p>
But in this particular case, it is empirically the NHL bosses who have destroyed this beautiful game. The NHL owners have taken a terrifically exciting sport--nothing is better live--with reservoirs of support in the Northern U.S. and Canada, and turned it into something grotesque.<p>
The NHL's journey is like Robert DeNiro's Jake Lamotta in <i>Raging Bull. </i>At the beginning, he's rough around the edges, but also magnetic, lean, and lethal. By the end, he's a bulbous, repellent, gassy, clown--a human car wreck. This--in all its ugliness--is the NHL.<p>
Their road to Armageddon began 13 years ago when they hired a slick National Basketball Association marketing whiz named Gary Bettman to be their commissioner. Bettman stated proudly that he had never set foot in an NHL arena, but knew how to "grow" the sport.<p>
Bettman took one look at this blue-collar league built on the backs of hardscrabble French Canadians, toothless grins and rabid fans, and recoiled. He examined its base in Northern de-industrializing cities and shook his head at the absence of revenue streams to suck dry. <p>
He saw the future of ice hockey and, unfathomably, saw Dixie. Bettman expanded the league to 30 teams, putting the sport in places like Nashville, Tenn., Atlanta, and Raleigh, N.C.<p>
The NHL owners sat back and collected hundreds of millions of dollars in expansion fees, giving out fat contracts along the way, with no thought to the long-term consequences. Predictably, these new revenue streams were shockingly shallow. The big national TV contract Bettman promised never came, and the NHL was left with unknowable new teams like the Hurricanes, Coyotes and Predators playing in half empty arenas.<p>
As sportswriter Dan Wetzel put it, "There is no denying that under [Bettman's] stewardship the NHL has been run into the grave. The league has been mismanaged, misplaced, overexpanded and overpriced, all because Bettman turned his back on the core fans--believing there was a pot of television and corporate (fool's) gold at the end of the small market rainbow."<p>
The money wasn't there. The attendance, which accounted for 80 percent of revenues, was down. Now with the sport in hideous shape, the owners, after a decade of overspending on marketing, arenas and players, are desperately looking for their profit margins. They are looking in the pockets of the players.<p>
Even though the union offered an unheard of 24 percent pay cut across the board to save the season, this was not enough. The owners have decided to risk the league on what is called a "hard cap." A "hard cap" is when owners decry the evil--for the first time in their lives--of the free market and demand external restraints on their ability to spend. This is an outrage players have every right to stand against.<p>
Why should there be a cap on what owners can pay them? This would be unacceptable to the owners and their ilk in every other walk of life. We don't see a hard cap on the price of a computer or car or a fancy meal.<p>
NHL union chief Bob Goodenow caved on the hard cap at the end, but Bettman snubbed him, more concerned with making sure the season stayed canceled than saving it with an 11th-hour deal. Why not? After all, several owners have said that the sport hemorrhages so much cash they actually lose less money with the game shut down than if they were paying salaries.<p>
Where this goes from here is difficult to say. The owners have been left with dusty jerseys, melting ice and a Panglossian belief that everything will be alright once they get their precious hard cap. Unfortunately they may find that if a hockey puck falls on the ice and no one is there to see it, it will fail to make a sound.<p>
