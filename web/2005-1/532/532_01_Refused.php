<font face="Times New Roman, Times, serif" size="5"><b>They refused to fight Bush's war for oil</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 25, 2005 | Page 1</font><p>

<font face="Times New Roman, Times, serif" size="3">CAMILO MEJIA is free after going to jail for the "crime" of resisting the U.S. war for oil and empire in Iraq. Mejia was freed February 15 from a military prison where he was serving a one-year sentence following his conviction for desertion.<p>

