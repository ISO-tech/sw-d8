As a staff sergeant in the Florida National Guard, Mejia was part of the invasion of Iraq. After a two-week furlough in October 2003, he refused to go back to his unit in Iraq because he believed the war was unjust.<p>
"The justification for this war is money, and no soldier should go to Iraq and give his life for oil," Mejia said before his trial last May. "I have witnessed the suffering of a people whose country is in ruins and who are further humiliated by the raids, patrols and curfews of an occupying army.<p>
Camilo's stand inspired antiwar activists and won him supporters around the world. Now, he is free--and determined to continue speaking out for peace and justice. "I want to thank all the people and all the organizations who have supported my family and me throughout this most difficult time in our lives," he said in a statement. "I am now free from prison, but it was because of all of you that I remained a free man during my incarceration."<p>
Mejia was one of the first U.S. soldiers to go public with his refusal to fight in the U.S. war, but he certainly wasn't the last. Currently, Pablo Paredes, a Naval petty officer third class who refused to ship out on the USS Bonhomme Richard for the Persian Gulf in December, is waiting to hear if he will be given conscientious objector status--or face military charges.<p>
Sgt. Kevin Benderman, a 10-year veteran of the Army, refused in January to deploy to Iraq for a second combat tour of duty. He reports to work daily at Fort Stewart in Georgia while awaiting word if he will be court-martialed. <p>
Benderman has faced abuse for the stand he took. A military chaplain who was supposed to discuss his application for conscientious objector status wrote to Benderman that he "should be ashamed of the way you have conducted yourself." The first sergeant of Benderman's unit denounced him as a coward.<p>
But cowardice has nothing to do with it, says another military resister--Carl Webb, of the Texas National Guard, who also refused to deploy to Iraq. "Were the guys who fought in the Confederate army cowards when they deserted?" Webb told a reporter from the <i>St. Louis Post-Dispatch. </i>"Were the guys who fought in the Nazi army cowards when they deserted? I'm not a pacifist. There are times I would fight in a war. I won't kill if I feel I'm on the wrong side. This is a war about oil and profits. It's not about bringing democracy to anybody."<p>
For every Camilo or Pablo or Kevin, there are many more service members opposed to the U.S. war and occupation who stay silent because they "don't want to risk going public," Paredes told <i>Socialist Worker </i>in an interview. "This is mainly because they won't get a voice if they do. The mainstream media won't give them a voice. <p>
Between 5,000 and 6,000 military personnel are officially listed as deserters, meaning they have been "absent without leave" from their unit for more than 30 days. That's twice as many deserters as the Pentagon's estimate in 2003. The GI Rights Hotline, a nonprofit organization that advises service members, says its annual number of phone calls was up to 32,000 last year--twice as many as in 2001.<p>
Bitterness with the military runs deep among personnel who did deploy to Iraq--as the crisis of the U.S. occupation has grown deeper. As one soldier with just a week left on his tour wrote to the newsletter <i>GI Special</i>: "I can still see the sparkle in my recruiter's teeth when he lied straight to my face. Soon, I will be on stage speaking out against this sick war. Until then, I think you guys are doing a fantabulous job."<p>
On March 19, the second anniversary of the invasion, antiwar veterans, military families and other activists will travel from around the country to Fayetteville, N.C.--the home of Fort Bragg--for another demonstration. Building the biggest possible turnout for this protest--and the many others planned for cities around the country--is the way to stand behind the soldiers and resisters who have said "no."<p>
Our message to Washington is simple: Bring all U.S. troops home now!<p>

