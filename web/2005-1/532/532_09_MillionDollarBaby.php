<font face="Times New Roman, Times, serif" size="4">Oscars favorite <i>Million Dollar Baby</i></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Hollywood plays it safe</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Brian Jones</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 25, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Million Dollar Baby, </b></i>directed by Clint Eastwood, starring Hillary Swank, Clint Eastwood and Morgan Freeman.</font><p>

<font face="Times New Roman, Times, serif" size="3">"ALWAYS PROTECT yourself" is Rule Number One in Clint Eastwood's latest film and Oscars favorite, <i>Million Dollar Baby.</i><p>
The trouble is that Frankie Dunn (played by Clint Eastwood) has become overprotective of the boxers he trains in his downtown Los Angeles gym, the "Hit Pit." Fighters learn a great deal from him, but move on if they want to move up, since he's reluctant to let them take the risks that are necessary to win title fights.<p>
The loss of his most promising trainee to other management is the occasion of the arrival of Maggie Fitzgerald (played by Hillary Swank) in the Hit Pit, a dirt-poor girl from Ozarks, who comes from "trash," but is determined to be trained by Frankie.<p>
At first he rebuffs her--"I don't train girls," he says--but she is encouraged by his only employee and long-time friend, Scrap (Morgan Freeman), an ex-boxer himself. Of course, Frankie eventually relents and at that point the film becomes, as Eastwood himself puts it, a father-daughter love story. Maggie is the daughter that Frankie wishes he had (his real one is estranged), and Frankie is the father that Maggie lost.<p>
The structure of <i>Million Dollar Baby</i> is remarkably unremarkable. Its narrative is relatively straightforward and relies on strong character relationships instead to deliver its punch (my apologies).<p>
Eastwood, Freeman and Swank all give strong performances. Tom Stern's stark photography and Eastwood's own original music, often in the form of a lone blues guitar, gave the film a haunting, timeless quality.<p>
That its "rags to riches" journey is interrupted by tragedy, however, does not serve as a social critique of the American Dream. And while Frankie is clearly a "free thinker" in the eyes of his local priest, and the characters grapple with the question of euthanasia, the film is not ultimately about religion or euthanasia.<p>
Consequently, despite the attacks it's received from right-wing pundits like Fox's Bill O'Reilly, <i>Million Dollar Baby</i> isn't really deserving of political praise from the left, or of political wrath from the right. Eastwood, we can't forget, lent the tough-on-crime backlash a Hollywood gloss with his <i>Dirty Harry</i> movies in the 1980s.<p>
So, while Eastwood has in <i>Million Dollar Baby</i> undoubtedly made a film that displays his ability to tug on our heartstrings, the politics of the film are no departure from the mainstream. That the protagonist is a member of the working poor makes her sympathetic, but only because she's different from the rest of them. Her family is portrayed as heartless, selfish and lazy.<p>
While Maggie trains, a poster in the background reads, "Winners are simply willing to do what losers won't." In the context of the 2005 Oscars, where the fantastically popular documentary <i>Fahrenheit 9/11 </i>has been snubbed altogether, the "winners" are bound to be a set of politically conformist million-dollar films.<p>
Eastwood and Hollywood have learned Rule Number One: "Protect yourself."<p>

