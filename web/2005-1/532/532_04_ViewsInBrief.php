<p><font face="Times New Roman, Times, serif" size="4"><b>U.S. companies are complicit</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IT IS so encouraging to read an article recognizing the essence of what is going on in occupied Iraq as an antidote to the liberal drivel which tries to cover up the monstrosity of the war by the excuse of a Mickey Mouse election ("Supporting Iraq's right to resist occupation," January 21).<p>
The point is, though, as socialists, aren't we just a little bit ashamed of ourselves and our inability to agree "among the left" and hence organize cohesively, such that the only hard opposition to U.S. military attacks on poor countries is "armed men and women fighting in the rubble of their homes"? Nationalistic and religious-based opposition, rather than socialist and internationalist?<p>
One task that <i>Socialist Worker</i> could usefully undertake would be to help the spreading of a boycott of U.S. goods which is now growing, and list the big U.S. companies in Britain and elsewhere--many of which trade under a disguised name so as to hide their origins.<p>
Surely, for example, nobody needs to drive Fords or Vauxhalls. Why not produce a top 100 list of companies to boycott? Destroy confidence in the dollar and the profitability of the U.S. system, and, without a drop of blood being shed, the whole ugly system will crash to pieces.<p>
With more wars on the horizon for sure, essays in journals are inadequate as a means of sorting out the problem (fiddling while Rome burns?).<br>
<b>T.G. McAllan,</b> Manchester, United Kingdom<p>

