<font face="Times New Roman, Times, serif" size="4"><b>Looting from us for the war</b></font><p>

<font face="Times New Roman, Times, serif" size="3">FIRST, I would like to compliment you on your upfront accounts about issues in our society today. Thanks for your dedication and support for American workers.<p>
I work for the Department of the Navy as a civil servant. The present salary schedule is based upon step increases, which occur every two to three years, depending on time in service. We have just been informed that the system will be changed as early as July 2005 to a performance-based merit raise system. <p>
If I truly believed that I would get a raise based on my performance, this would be great, as I am very competent in my job duties. However, I believe that this is only a way for the uppermost officials to give themselves raises and withhold any pay increases for the rest of us. When the yearly evaluation comes around, I believe we will be told, "There is no money in the budget for raises this year."<p>
I believe the administration is going to loot the funds for a war or some other activity that they feel is more worthy, and give themselves hefty raises. It isn't enough to have to worry about pending outsourcing, now there's this--just another way to reduce the salaries of the workers. <p>
Also, did you hear about how San Diego County supervisors tried to give themselves 25 percent raises? Fortunately, they got caught. Considering that libraries only operate minimal hours and parks and recreation have taken hard hits, it was especially presumptuous that these supervisors attempted to give themselves such hefty raises.<p>
Thanks for listening.<br>
<b>Beth,</b> from the Internet<p>
