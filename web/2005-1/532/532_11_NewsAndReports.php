<p><font face="Times New Roman, Times, serif" size="4"><b>Equal marriage is a right</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Around 50 people protested Mayor Michael Bloomberg's re-election campaign kickoff February 15 in Times Square because of his decision to appeal the recent State Supreme Court ruling that it is unconstitutional to prohibit gay and lesbian couples from marrying. <p>
While Bloomberg claims that he is personally pro-gay marriage, actions speak louder than words. <p>
With the court's ruling, the road is clear for the city to start issuing same-sex marriage licenses--if Bloomberg allows it. Granting such licenses now would present an obstacle to any future court battle to challenge the Supreme Court ruling. But Bloomberg's decision to appeal has put the brakes on.<p>
Our signs and chants of "Gay, straight, black, white, marriage is a civil right" must have disrupted the staged affair, as cops parked two paddy wagons in front of us to shield the event. While police succeeded in channeling us into a "protest pen," some passers-by in Times Square nevertheless joined us and took up our chants. <p>
Last Valentine's Day in <b>San Francisco </b>was an historic celebration for hundreds of gay couples that had been issued marriage licenses. <p>
Since then, the movement has been dealt heavy blows by same-sex marriage bans and scapegoating by liberals who claim that "it isn't the right time to raise this issue." This was reflected in the much more somber Valentine's Day celebration this year. <p>
A number of couples came to city hall to put a human face on the issue of gay marriage. Emotions were certainly high as the couples filed into the county clerk's office to ask for marriage licenses that they knew would be denied, while others outside held a press conference.<p>
Frank, a speaker at the press conference, explained that he has a dangerous job and that because his union doesn't grant his partner the same rights as a spouse, he worries about his husband's status should anything happen to him on the job. And in response to some liberals who are queasy about supporting the right of same-sex couples to marry, Diane, who is part of a class-action lawsuit demanding that California grant gay marriage licenses, put it very simply: "It is always the right time to fight discrimination."<p>
We have a responsibility to renew activism around this demand for equal rights.<p>
<i>Mitch Day and Jessica Hansen-Weaver contributed to this report.</i> <p>

