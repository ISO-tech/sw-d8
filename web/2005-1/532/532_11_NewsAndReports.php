<font face="Times New Roman, Times, serif" size="4"><b>Defend Lynne Stewart</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Sarah Grey</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--"The idea of not fighting is unthinkable," civil rights defense attorney Lynne Stewart proclaimed February 17, as a cheering crowd of 400 rose to its feet to greet her. <p>
Stewart, who was convicted February 10 of "aiding terrorism," faces a possible 30-year prison sentence. Her crime? Defending her client, Omar Abdel Rahman, an Arab man being held without bail under the USA PATRIOT Act.<p>
Stewart was joined by members of her defense team, fellow civil rights attorneys, and activists from the National Lawyers' Guild (NLG), which declared February 17 a "National Day of Outrage." As law student Laura Raymond triumphantly reported, the NLG's 98 chapters around the country organized pickets, speakouts and tablings to promote Stewart's cause.<p>
The tactics used to convict Stewart, such as bugging her conferences with her client, undermine the right of accused people--who are overwhelmingly poor people of color--to be represented by counsel, and they create a climate of fear intended to keep lawyers from providing legal defense to the people who need it most.<p>
While this case is painful for Stewart personally, she sees it as a way to galvanize activists to organize. "I am happy to serve as the foil for that organization if it pushes the movement forward," she said.<p>
Stewart's supporters are organizing a letter-writing campaign to convince the sentencing judge to impose a light sentence. Stewart also called for a mass demonstration on September 23, the day of her sentencing hearing, and pointed out that the civil rights of detainees and prisoners must be vigorously defended not only by attorneys, but by ordinary people organizing and demanding change. <p>
Fight for Lynne Stewart to go free--and challenge the Bush administration's assault on all civil rights.<p>
<i>For more information about Stewart's defense campaign, go to <a href="http://lynnestewart.org" target="_blank">lynnestewart.org</a>.</i> <p>
