<font face="Arial, Helvetica, sans-serif" size="2"><b>NO TO WAR AND OCCUPATION</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"I don't want to be a part of this"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 25, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">SACRAMENTO, Calif.--On February 15, 100 antiwar activists showed up outside of a home here to counter a pro-war vigil called by conservative radio host Mark Williams and Move America Forward, a group that supports the racist war on terror.<p>
The controversy began when Virginia and Steve Pearcy displayed an effigy of an American soldier on their home with a message reading "Your tax dollars at work." The antiwar display was ripped down within days after Williams and other radio show hosts stirred up media attention around the issue.<p>
Determined to get their message across, the Pearcys put up another figure with the slogan "Bush lied, I died." Within two days, the second display was ripped down and replaced with a Bush campaign sticker. Incredibly, the Pearcys are now facing a hate-crime charge for displaying the effigy, while the vandal who trespassed and stole the first figure was charged with a misdemeanor.<p>
"We are supposedly spreading democracy," one neighborhood resident told <i>Socialist Worker</i>. "I thought democracies allowed free speech." <p>
In <b>Los Angeles, </b>Conscientious objector Pablo Paredes spoke out at a February 17 panel that focused on militarism in schools, recruitment on campuses and what students, teachers, veterans, military families and activists can do to organize against Bush's wars. The panel was sponsored by the International Socialist Organization and endorsed by several community groups. <p>
A leading representative from the Coalition Against Militarism in our Schools (CAMS) opened by expressing alarm about the increasing number of recruiters on Los Angeles-area campuses, and the organization Esperanza Para Familias Militares (Hope for Military Families) gave a presentation in Spanish. "I don't want to be a part of a ship that's taking 3,000 Marines over there, knowing a hundred or more of them won't come back," Paredes told the crowd. "I can't sleep at night knowing that's what I do for a living." <p>
In <b>New York City, </b>about 200 people attended a Columbia University Senate town hall meeting February 15 to debate a proposal to bring ROTC back to Columbia's campus. <p>
ROTC was expelled from Columbia in 1969 as a result of student-led building takeovers protesting the Vietnam War. Last year, Advocates for Columbia ROTC and Students United for America put forward a joint proposal to bring ROTC back. <p>
The students, professors, and alumni who attended the town hall were evenly split between support and opposition of the proposal. Opponents of the proposal--including the Columbia Antiwar Coalition, the International Socialist Organization and the Gay and Lesbian Law Students Association--argued that "discrimination based on sexual orientation is not something we want at our university," and that requiring students to commit to military service in order to get money for school is a form of "legalized bribery." <p>
In <b>Madison, Wis.,</b> at the University of Wisconsin, 25 students and supporters of Stop the War confronted Navy recruiters at a student job fair in the student union February 16. Protesters chanted "Recruiters off campus" and "Money for education, not for war" until campus police threatened to arrest them if they continued chanting. <p>
Police told protesters that they could continue to hand out literature and hold signs, but later, the building manager claimed that, according to building rules, protests are only allowed in the building's lobby. Protester Blake Trimbell was cited for "disorderly conduct" resulting in a $274 fine. <p>
Stop the War is planning more actions, including a teach-in and a walk-out.<p>
<i>Contact University of Wisconsin Union Director Mark Guthier at 608-262-2263 or <a href="mailto:mcguthier@wisc.edu">mcguthier@wisc.edu</a> to demand the fine against Trimbell be dropped. Bill Linville, Alison McKenna, Katie Miller, Jenny Olson and Stephanie Schwartz contributed to this report.</i> <p>

