<font face="Times New Roman, Times, serif" size="4">Left-wing AFSCME official in NYC removed undemocratically</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The struggle for DC 1707</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar and Shaun Harkin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 25, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--An outspoken official in the public-sector union AFSCME has been removed from office undemocratically amid a dispute over a contract ratification vote for New York City day care workers.<p>
Brenda Stokely, president of AFSMCE District Council (DC) 1707, was voted out of office by union delegates in January after DC 1707 Executive Director Raglan George alleged that she had held her office in violation of the union constitution. The removal of Stokely--who is co-chair of New York City Labor Against the War, and was an organizer for last summer's Million Worker March--follows George's decision to fire two other officials aligned with her, Gloria Jackson and Chuck Mohan.<p>
Stokely, Jackson and Mohan had been part of a reform slate with George that won office in May 2002.<p>
Stokely's political views--including her criticisms of the Democratic Party, speaking as a leader of the Labor Party--have long been unpopular with the AFSCME hierarchy, as is her advocacy of union democracy. Zionists within AFSCME locals have also criticized her pro-Palestinian and antiwar views.<p>
It appears that the AFSCME leadership's longstanding hostility toward Stokely dovetailed with the opposition of her Zionist critics, as well as George's ambition to consolidate his grip on DC 1707.<p>
At the heart of the controversy is a concessions contract for day care workers pushed by George and opposed by Stokely, Jackson and Mohan.<p>
DC 1707, which represents 23,000 day care and home care workers at centers run by private, nonprofit agencies, is far smaller than AFSCME DC 37, which represents more than 100,000 city workers. But DC 1707 upstaged its bigger counterpart by organizing a one-day strike of day care workers in February 2003, and a three-day strike in June 2004, in the fight for a new contract.<p>
Nevertheless, the new contract put together this month has many weaknesses. It calls for separate, but simultaneous raises totaling 12 percent effective January 1, 2005 in a 63-month contract retroactive to January 1, 2001. Another 2 percent wage increase is set for April 2005--but only if the union agrees to further concessions on productivity. New hires would be paid 11 percent less than the final wage rate as of April.<p>
The contract also lacks any guarantees of retroactive pay for the period since 2001. Instead, the union is relying on Democrats in the state legislature to provide $20 million for such pay at a later date, "a policy that's already failed twice," according to Stokely--once under former Mayor Rudolph Giuliani and again under the current mayor, Michael Bloomberg. The result, said Stokely, will be no compensation for a four-year wage freeze.<p>
Meanwhile, day care centers are closing because low pay means they can't retain the ratio of qualified teachers for the kids. "Our day care centers were the first to be organized in the country," Stokely told <i>Socialist Worker</i>. "This came out of the African American struggle as a demand. That's one of the reasons we are raising this up" in the union and the community.<p>
With new union elections coming up in May, George was acting to "clear the decks" of potential rivals--and critics of the concessionary contract put before the membership, Stokely said.<p>
To silence her, George used his powers as executive director--an administrative position in the AFSCME structure that allows officials to remain in office for years. George got the DC 1707 delegates to vote to increase his pay from $70,000 to $100,000--and used patronage to get the votes to remove Stokely, she said.<p>
George claimed that Stokely is a union employee, and is therefore ineligible to hold the presidency under union rules. In fact, Stokely had been given a stipend from the union after being laid off from her job in 2003--and in any case cannot be removed without facing charges.<p>
On February 14, Stokely, Jackson and Mohan filed a federal lawsuit to try to overturn George's actions in removing them under the Labor Management Reporting and Disclosure Act.<p>
The following day, according to Stokely, George intervened in a contract ratification meeting of AFSCME Local 205, which represents about 7,000 day care workers. Witnesses say that George took over the meeting from Local 205 President Glen Huff to call for a stand-up vote on the contract--violating union procedure that calls for a formal ballot.<p>
Stokely and her supporters vow to continue the fight. "I am a delegate of Local 215, and I will be at the next meeting," she said.<p>
