<font face="Times New Roman, Times, serif" size="3"><b>WHY DID you decide to refuse to go to Iraq?</b> <p>

THE REASON is twofold. First of all, I'm opposed to war in general. Secondly, this illegal war in Iraq was a wakeup call. <p>
Before the war, I was in Japan, so far removed from the idea of war. Once it began, and I found out that my ship would be transporting Marines to their possible death, I decided I had to take a stand and refuse to board. <p>

<b>WHY DID you join the military?</b> <p>

WHEN I turned 18, I started to attend community college in the Bronx. Fortunately, I was able to get a scholarship to help pay for it. Unfortunately, my tuition increased, and my scholarship didn't. I couldn't afford to continue and dropped out. <p>
It was at this point that I remember getting constantly hounded by military recruiters, who promised that I could get money for college and pursue my education later. I was "happy go lucky" and ignorant of the policies of this country, and thinking only about educational opportunities. Since my parents had served in the military, I figured I would go the same route.<p>

<b>HOW DO you respond when people say you "signed an oath" and "shouldn't question orders?</b> <p>

I REMIND them that part of that oath is that if we receive illegal orders, we should disobey them. This is according to Geneva Conventions. In fact, those of us who disobey orders to participate in an illegal war are staying more true to that oath then our leaders who participate. <p>
Let's say that I'm making the wrong decision. If the Nuremburg trials show us anything, it is that it's better to make this kind of mistake opposing a war than risk repeating these kind of atrocities by "following orders." But I don't think I'm making a mistake. <p>
More GIs are coming out against this war. Some, like Camilo Mejia, decided to take a public stand. Most who are opposed don't want to risk going public. This is mainly because they won't get a voice if they do. The mainstream media won't give them a voice. <p>
The media chooses to work within the established parameters of what the government is promoting. The people who own the media also own the government. They're on the same page, whether Democrat or Republican. <p>
That's why people choose other means--such as the nearly 6,000 who have deserted and the increasing number that aren't showing up to report for duty. The general population really has no idea, because the media doesn't talk about it. <p>

<b>HOW WOULD you describe the response to what you've done?</b> <p>

THERE HAS been an overwhelming response of support from the public. Greg Palast, a progressive journalist, came to speak recently to a crowd of several hundred. The moderator mentioned my presence, and the crowd responded with a loud standing ovation. I thought few people knew of me, I was amazed. <p>
On the military side, I expected a lot of antagonism, and at first, there was a lot of that from the families of my shipmates. This is likely because they were resentful of seeing their loved ones shipped out. But since then, people have been willing to talk, to hear me out. <p>
The sergeant at arms on the ship--the person in charge of taking me into custody when I turned myself in--is an interesting story. I saw him on the base another time, and we ended up having a 30-minute debate about this war and my actions. While we disagreed, it ended on a positive note, and he said he respected my point of view. Many other military personnel have sent me letters of support. <p>
I have grown very close with the family of Camilo Mejia. In fact, it is no surprise that so many Latinos GIs are coming out against the war. If you think about it, the history of U.S. imperialism in Latin America stays with us. It reflects on the way we understand U.S. military action in places like Iraq. <p>

<b>WHAT'S NEXT for you?</b> <p>

I'VE LEARNED a lot in the last few years and opened my eyes to what's going on. I used to not know, not care. I just went through life not thinking about anything happening in the world around me. <p>
Now, I've started to learn how to learn--to dissect truth. You have to, since information is presented in such a distorted way. <p>
Because of this, many people in society don't really know what's happening in the Middle East. People have no idea that Palestinians have had their lands taken away from them, for instance. <p>
At this very moment I am waiting to see how my case goes. My [conscientious objector] package has been filed, and now it's in the military's hands. In the future, I want to become more active. I want to be a voice that helps people find truth. <p>

