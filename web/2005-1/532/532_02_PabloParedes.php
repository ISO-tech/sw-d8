<font face="Times New Roman, Times, serif" size="4">Pablo Paredes explains why he's resisting</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"I want to be a voice for truth"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 25, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">PABLO PAREDES made his bold protest against the U.S. occupation of Iraq when he refused to ship out for the Persian Gulf in December on board the USS Bonhomme Richard.<i> Socialist Worker's </i>JUSTIN AKERS talked to Pablo about why he decided to become a military resister.<p>

