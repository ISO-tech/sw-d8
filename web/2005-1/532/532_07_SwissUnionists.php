<font face="Times New Roman, Times, serif" size="5"><b>Defend Swiss unionists</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 25, 2005 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">FIVE OFFICIALS and activists of a major union in Switzerland have been threatened with up to three years in prison for nothing more than exercising their right to protest. Supporters of the five say this is an unprecedented attack on union rights in a country that claims to be a model of "democracy."<p>
The five are all well-known figures in the Swiss Public Service Union (SSP), and three are members of left-wing political groups. The accusations against them stem from a picket in Geneva last May that called on bus drivers to support striking public-sector workers--and shut down the city's transit system for part of one day.<p>
The five SSP officials and activists are accused of "impeding and instigating the hindrance of the public service and the general interest," according to a statement in their defense.<p>
The judge in charge of this case is the same one who investigated police violence against demonstrators at a G8 summit held across the border from Geneva in Evian, France--and who found no cause to prosecute police who shot and wounded one demonstrator with a percussion grenade.<p>
<i>Call on the Swiss authorities to drop all charges against the five unionists by writing to:: Attorney General of the Republic and Canton of Geneva, Daniel Zapelli, Law Courts, Places Borough-of-Furnace, 1204 GENEVA, Switzerland. Send copies of these messages to the Geneva press at: <a href="mailto:redaction@lecourrier.ch">redaction@lecourrier.ch</a> and <a href="mailto:redaction@tdg.ch">redaction@tdg.ch</a>, as well as the Public Service Union at: <a href="mailto:sspge@vtxnet.ch">sspge@vtxnet.ch</a>.</i><p>

