<p><a name="Side"></a><font face="Times New Roman, Times, serif" size="4"><b>Thanks for telling my side</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I WOULD like to thank <i>Socialist Worker,</i> and John Osmand in particular, for telling my story ("Bigotry at Blue Cross," May 6). It's been a rather lonely vigil, and John's interest and encouragement have meant so much to me. <p>
I've also been gratified by the support I have received from many of my former co-workers, even those who only dared whisper their approval sotto voce. <p>
From the beginning, I've felt that this story wasn't just about me. Diversity is a foreign concept at Blue Cross and Blue Shield of Rhode Island. In my 17 years there, I never even heard anyone in authority mention the word, much less implement a policy to achieve it. As I look back at my time at that "institution," I can't help but wonder at my own naivet&eacute;. <p>
From the outset I hoped to, at least, raise awareness so that others would be forewarned at what they might expect from that establishment. <br>
<b>Joe Giarusso,</b> Providence, R.I.<p>

