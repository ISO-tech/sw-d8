<p><a name="Sadr"></a><font face="Times New Roman, Times, serif" size="4"><b>Is Sadr a real alternative?</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHUCK STEMKE writes in support of Iraqi Shiite leader Moktada al-Sadr that "though he is high on the clerical hierachy, he sees the need to build national unity, and therefore opposes institutionalizing religious law" ("Alternative to the occupation," April 29). Some recent actions of Sadr's followers would seem to contradict this analysis.<p>
On March 15, Sadr supporters attacked a group of students from Basra University while on a picnic. Apparently, what bought on the attack was that the outing consisted of male and female students, and crossed ethnic and religious lines. A young woman, who appears to have been singled out for being a Christian, was physically attacked, and when a male friend attempted to defend her, he was shot dead by Sadr's supporters. <p>
The students carried out a six-day strike in protest against these attacks. The Federation of Workers Councils and Unions in Iraq issued a statement condemning the attacks and supporting the students' strike. Because of widespread public support of the students, a representative of the Mahdi Army, which supports Sadr, was forced to issue a public apology for the attacks in a leading Basra newspaper.<p>
The Worker-Communist Party of Iraq (WCPI), opponents both of the U.S.-led occupation of Iraq and the armed resistance to that occupation, claims that it learned--by way of a supporter in the Basra organization of Sadr's organization--there was a plan to "kidnap and murder" leading WCPI cadre in Basra. If this is true, it would indicate that those in favor of an Islamic republic would most likely carry out a campaign to eliminate the Iraqi left, as was done in Iran by the Islamic regime in that country.<p>
While supporting forces in Iraq who actively oppose the U.S. occupation of their country, we should also condemn attacks against leftists and other advocates of secularism by conservative forces in Iraq.<br>
<b>Ken Morgan,</b> Madison, Wis.<p>

