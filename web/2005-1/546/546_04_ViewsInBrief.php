<font face="Times New Roman, Times, serif" size="4"><b>Media silence on racist thugs</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AS ONE of the approximately 300 antiracists who mobilized to prevent the neo-Nazi "White Revolution" from disrupting the May 8 Holocaust memorial service in Boston, I write to thank you for Sriram Ananthanarayanan's outstanding report ("Boston boots the Nazis," May 13).<p>
<i>SW</i>'s headline stands in stark contrast to the absolutely mystifying report in Boston's leading corporate newspaper, the <i>Boston Globe</i>: "Demonstrations mar commemoration." The <i>Globe's </i>all-but-illiterate lead belied the corporate media's pretense of objectivity by implying a moral equivalency between racist hate-mongers and Holocaust deniers, on the one hand, and activists committed to building solidarity across the divisions created by the ruling class, on the other.<p>
Ironically, while we took to the streets to tell the racist scum they are not welcome in our commonwealth, Gov. Mitt Romney piously told reporters he wished the White Revolution thugs would "go back where they came from and bury themselves under the rocks that they crawled out from." That such were the sentiments of the chief executive officer of the state must be news to Billy Roper, "chairman" of the fascist swine, who (according to Levenson and Allen) "praised the police for protecting his members."<p>
At the end of the day, the failure of the neo-Nazis to disrupt the memorial was due neither to the governor's self-serving rhetoric nor to the massive police presence protecting them. <p>
Rather, it was due to the counter-demonstration by members of what Levenson and Allen dismissively described as "communist, anarchist and antiwar groups." We turned out at short notice to say that "Never Again" requires the willingness to confront the fascists every time they dare to appear in public. As Ananthanarayanan rightly insists, "fascism grows precisely where it goes unopposed."<p>
In addition to exposing the vile politics of the "White Revolution," then, the anti-fascist mobilization also exposed the hypocrisy of the state's political establishment and, it would appear, of the <i>Boston Globe</i>.<p>
Against such a background, Ananthanarayanan's report underscores the vitally important role <i>Socialist Worker</i> plays in grassroots efforts to rebuild a left political alternative in this country. Thanks again for <i>SW</i>'s work in highlighting the struggles for social justice that the corporate media self-servingly ignores or distorts.<br>
<b>Mark Clinton,</b> Northampton, Mass.<p>

