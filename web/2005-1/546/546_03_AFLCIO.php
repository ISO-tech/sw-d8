<font face="Times New Roman, Times, serif" size="5"><b>Do union leaders have a strategy?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 3, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">RIVAL UNION leaders have plenty to say about the future of the movement leading up to July's AFL-CIO convention. But none are doing anything to stop the House of Labor from burning down.<p>
Last month, the firestorm spread to the auto industry, where United Auto Workers (UAW) leaders agreed to Ford's effort to reacquire 14 auto parts plants from its former Visteon division in a complicated deal that creates a holding company. which would then sell off the plants.<p>
In the short run, the deal will eliminate 5,000 of 17,500 union jobs--and accelerate the massive wage cuts in the auto parts industry agreed to by UAW leaders in the 2003 contract. However, UAW officials are giving members little time to debate the deal, calling for a vote on the agreement beginning June 1 with little time for discussion.<p>
The agreement will also put Visteon workers' pensions in peril. While a small number may be able to transfer back into Ford plants, there's no telling what will happen to the retirement plans of workers in plants that are sold off to third parties in the future.<p>
A similar battle is underway at the former Boeing aircraft plant in Wichita, Kan., where the Canadian company Onex has bought the facility and aims to extract big concessions from the International Association of Machinists (IAM).<p>
The IAM is also in the crosshairs of United Airlines management, which is threatening to use bankruptcy proceedings to tear up union contracts. Such a move would spark yet another round of demands for wage and benefit cuts that have hammered unions in the airlines--the most heavily unionized industry in the private sector.<p>
The IAM and other unions have threatened to strike if United tries to cancel their contracts. But there's been next to no preparation for such a fight in the wider labor movement, making what would be a high-stakes battle even more difficult.<p>
Despite the intensity of the corporate onslaught, there's been little discussion of how to hold the line--even as the rhetoric heats up over who will lead the AFL-CIO or whether it will split.<p>
The IAM and UAW, for example, support AFL-CIO President John Sweeney for re-election. But rather than put up resistance or offer any vision for the future, they're clinging desperately to what they call their "partnership" with employers.<p>
Sweeney's challengers, led by Service Employees International Union President Andrew Stern, claim to offer a new direction--but so far that's consisted of demanding rebates from the AFL-CIO for new organizing and mergers to create bigger unions. And Stern's most powerful ally, Teamsters President James Hoffa, had little to say last month when UPS, the biggest Teamster employer, announced its buyout of Overnite, a viciously anti-union freight company that defeated a long, badly run Teamster strike for a union contract on Hoffa's watch.<p>
Meanwhile, both sides of labor's divide appear ready to continue their close--and subordinate--relationship to the Democratic Party, rather than explore an independent alternative.<p>
It's hard to predict who will come out on top in labor's internal battle. But what's certain is that the real fight to revive the labor movement has yet to begin.<p>


