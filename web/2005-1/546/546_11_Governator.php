<font face="Times New Roman, Times, serif" size="4">Thousands protest California's public-sector cuts</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Protesting the Governator</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Snehal Shingavi</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 3, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">SOME 15,000 students, teachers, nurses, firefighters and other unionized workers descended on Sacramento and Los Angeles May 26 to protest California Gov. Arnold Schwarzenegger's budget cuts, attacks on workers and his plans for a special election.<p>
In what was likely the largest union rally in California in years, several unions organized busloads of people from all over California to show their opposition to Schwarzenegger's growing attacks on the public sector and public employees. The California Nurses Association, for instance, has been waging a public fight against the governor for the past several months, taking aim at Schwarzenegger's proposal to eliminate nurse-to-patient ratios as well as his sexist attacks on the union's largely female membership.<p>
The protests have had a serious impact. Not only have Schwarzenegger's approval ratings dropped to new lows, but a poll released last week by the Public Policy Institute for California found that 62 percent of voters are opposed to Schwarzenegger's call for a special election.<p>
That special election would seek a constitutional cap on state spending to force down the amount of money the state must provide schools as well as a measure that would weaken tenure protections for public school teachers. The special election itself would carry a price tag of about $80 million.<p>
This new attack comes on the heels of an already shrinking state budget and Schwarzenegger's decision to renege on a number of promises he made when he temporarily suspended Proposition 98, which guaranteed minimum funding for public schools.  <p>
Both of California's teachers' unions came out in opposition to Schwarzenegger. Schools gave up billions of dollars in 2003 to alleviate California's budget crisis, and now Schwarzenegger has refused to repay their sacrifices. Other unions like the Service Employees International Union and the firefighters unions came out in opposition to Schwarzenegger's plans to privatize public pensions.<p>
More than anything else, the protests last week demonstrated that collective union action can help shape the political climate and build broader opposition to Schwarzenegger's legislative priorities.<p>

