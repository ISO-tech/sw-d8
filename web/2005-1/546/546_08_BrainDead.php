<font face="Times New Roman, Times, serif" size="5"><b>Why is Bush getting away with murder?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 3, 2005 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">THERE SEEMS to be a disconnect in politics in the U.S. today.<p>
Check any opinion poll this month and you will find that President Bush's popularity rests somewhere in the mid-40 percent range. On specific subjects, like privatizing Social Security, Bush's ratings reach Nixonian levels of unpopularity, with about one in three Americans expressing support. And a growing majority believes the war in Iraq wasn't worth it.<p>
Bush's and Congress's crashing support should be a signal that Americans are looking for an alternative to the right-wing politics that dominated the country. Yet, you'd be hard-pressed to find among the liberal organizations and the Democratic Party a sense of anything other than a feeling of siege and resignation. Why?<p>
Usually liberals explain their impotence by blaming the American populace (or "the heartland") for being too right-wing to realize just how great liberal ideas are. Yet a "veteran Republican who has close ties to the White House," quoted by the May 24 <i>New York Times</i> comes closer to the truth.<p>
"The only reason [Bush is] still up there in the 40s is that the Democrats are really brain-dead and have nothing positive to put on the table," the operative said. "This is more than a rough patch; it's a dark moment right now for Bush."<p>
So if even Bush's supporters can see that a real opposition could cripple the administration, why can't the so-called "opposition" see it?<p>
For one thing, describing the Democrats as an "opposition" is more of a parliamentary technicality (they are, after all, the minority in Congress) than it is a description of their political orientation. In a political system whose longevity and stability is based on the rotation in office of two parties committed to the capitalist status quo, opposition occurs.<p>
In the period of the Democratic dominance--the 1930s to 1980s--the status quo accepted some degree of state intervention in the economy and a modicum of social welfare programs. So the GOP largely accepted this and raised opposition on the edges of accepted status quo.<p>
Today, with the free-market neoliberal consensus largely established, it's the GOP that pushes the envelope and the Democrats that try to work around the edges.<p>
This simply means that the Democrats are accomplices in the Republocrat attacks on working people and their quest for empire abroad. Substantial Democratic support for the pro-business "bankruptcy reform" and "tort reform" and the 100-0 vote in the Senate for the latest blank check for the fiasco in Iraq shows this.<p>
The problem is that the Democrats then set the tone for the liberal constituent organizations like the National Organization for Women or even the AFL-CIO that supposedly connect Democrats to their "grassroots bases." These organizations have long ceased to act as independent mobilizers of their constituents to press the politicians to address crucial issues. Instead, they spend their efforts trying to tailor their organizations' activities to Democratic electioneering and to convincing their "bases" to keep the faith in the Democrats despite their many sellouts.<p>
This explains the seemingly inexplicable situation in which the Bush administration or an employer takes some outrageous action, and despite indications that the action is substantially unpopular, no organized resistance to it arises. <p>
This has the perverse effect of reinforcing conservative ideological hegemony. Until the discontent that exists in society expresses itself through some kind of organization and mobilization, Bush and the conservatives will continue to get their way. And many liberals will conclude that the mass of the population is too ignorant or conservative to stand up to it. <p>
But a necessary part of building a sustained opposition to these attacks is the development of a left that will organize grassroots opposition and will have enough ideological clarity not to surrender its independence. <p>
Over the next years, there will be many events--from the attacks on Social Security and abortion, to the continued disaster in Iraq--that will present opportunities for a left to organize opposition and to grow.<p>

