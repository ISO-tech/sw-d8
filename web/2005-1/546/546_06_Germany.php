<font face="Arial, Helvetica, sans-serif" size="2"><b>GERMANY</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Behind the SPD's election setback</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jeff Bale</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 3, 2005 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">A SURPRISE election defeat for Germany's Social Democrats (SPD) sent shock waves through the country last month, leading Chancellor Gerhard Schr&ouml;der to make a call for early national elections.<p>
Schr&ouml;der's announcement came in response to his party's trouncing in state elections in North-Rhine Westphalia (NRW), the most populous state in Germany and a main base of support for the SPD. The party took a dismal 37 percent of the votes. The conservative Christian Democrats (CDU) won an absolute majority of seats in the state parliament, with 45 percent of the vote. This will be the first time in 39 years that the SPD has not been in power in NRW.<p>
Aside from its size, NRW is an important political bell-weather. It is home to the country's coal and steel industries, as well as many of Germany's largest industrial operations. For this reason, NRW has been the backbone of the SPD's working-class base since the Second World War. Having lost this base in the state elections is nothing short of a disaster.<p>
The vote is not a working-class endorsement of the conservative CDU, but rather, a resounding rejection of the neoliberal policies of Schr&ouml;der's Agenda 2010, which the SPD has been pushing for several years.<p>
The most contentious part of Agenda 2010 is the Hartz IV Commission employment "reform" package. Named for the former Volkswagen executive who Schr&ouml;der asked to lead the commission, the deal is an attack on welfare policies that any U.S. politician would endorse.<p>
The most significant cut is in unemployment insurance. In a campaign reminiscent of Reagan's make-believe "welfare queens," the Hartz IV commission argued that too many Germans are living too comfortably on unemployment insurance. Thus, both the amount paid out and the length of payments are to be cut. <p>
Germany's unemployment rate has hovered around the 5 million mark for over a year. In NRW alone, there are some 1 million out of work--more than 12 percent of the population. Just last winter, the auto giant General Motors announced some 10,000 layoffs at a factory of its Opel subsidiary in Bochum--and the SPD did nothing.<p>
This explains the discontent among SPD voters. In a last-minute effort to mobilize working-class votes in NRW, Schr&ouml;der dispatched his party's number two, Fritz M&uuml;ntefering, who is considered to represent the party's left wing. M&uuml;ntefering made the media rounds in the last days before the election, making sharp attacks on big layoffs and big corporations. He even likened venture capitalists and their investment banks to "a swarm of locusts descending on a crop." <p>
But his tour to whip the base into shape was largely rejected as shrill opportunism--and many SPD voters simply stayed home during last month's vote.<p>
This is the context for Schr&ouml;der's decision to call early national elections. He clearly hoped to catch his main opposition, the CDU, off guard and gain a quick leg up in the campaign. In fact, the CDU is still fighting among itself about who to put forward as a candidate for chancellor.<p>
More important still is Schr&ouml;der's goal of pulling his divided party back together--using the argument of "lesser evilism" like the Democrats did in last year's presidential election in the U.S.<p>
In an important sign of the SPD's disarray, leading left-winger Oskar Lafontaine quit the SPD following the NRW election. Lafontaine has been known as a maverick within the SPD since he resigned his cabinet post in the first Schr&ouml;der government, over Schr&ouml;der's first push around cuts to the welfare system. <p>
Now, Lafontaine has quit the SPD altogether, and there is talk that he could be a candidate for a left-wing election bloc to counter the SPD. Already, he has mentioned running with the Party of Democratic Socialism--the remade East German Communist Party.<p>
By shaking a stick at the CDU, Schr&ouml;der hopes to win back disaffected progressive voters at the SPD's base. But Lafontaine's resignation is one sign of the potential for a left-wing alternative to develop to the SPD's neoliberal policies.<p>
Another sign is last summer's weekly demonstrations against Schr&ouml;der's Agenda 2010, which united unions, left-wing parties, socialist groups, church organizations and others. While they eventually dwindled, their size and the diverse cross-section of society that they attracted proved that there is a significant audience looking for a left alternative to the SPD program of tax cuts for corporations--and social cuts for workers. <p>

