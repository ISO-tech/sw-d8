<font face="Times New Roman, Times, serif" size="5"><b>Why you should join us at Socialism 2005</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 3, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">WHEN PEOPLE look back at the great struggles of the past, they remember the drama of large demonstrations or confrontations with the forces of the status quo. History remembers the 1963 March on Washington, where Martin Luther King gave his "I have a dream" speech--or the police assault on young civil rights demonstrators on the streets of Sheriff Bull Connor's Birmingham, Ala.<p>
But this can leave out the events that built the movement in the first place--as often as not, the formal and informal meetings where people came together to talk about what was wrong with society and to learn from each other about the politics and traditions of the struggle. Without these discussions, the movement couldn't have gone forward.<p>
On July 1-4 in Chicago, <i>SW </i>will cosponsor Socialism 2005, a four-day conference that aims to be a forum for education and debate for the movements of the future.<p>
There will be reports from activists on the front lines of today's struggles. But equally important are the courses and meetings that offer the opportunity to deepen our understanding about the world--and about the socialist tradition dedicated to changing it and winning a society of justice and equality.<p>
If you follow the course on <b>The Basics of Marxism </b>at Socialism 2005, it includes Phil Gasper--author of a forthcoming annotated edition of Karl Marx and Frederick Engels' classic <i>The Communist Manifesto</i>--on <b>The ABCs of Marxism. </b>Glenn Allen continues the course with a discussion of <b>Marx's Theory of Crisis, </b>and Shaun Harkin leads a session on <b>Why the Working Class Can Rule Society.</b> Other meetings in this course include <b>How Marx Became a Marxist, Is Marxism Eurocentric? </b>and <b>The Transition from Socialism to Communism</b>.<p>
Today, as the U.S. government wages war around the world in the name of "stopping terrorism," the course on <b>Imperialism and War </b>will be especially important.<i> International Socialist Review </i>editor Ahmed Shawki examines U.S. foreign policy after September 11 in <b>U.S. Imperialism: A New American Century. </b>Plus, <i>Socialist Worker </i>columnist and <i>ISR </i>associate editor Paul D'Amato will speak on <b>Revolutionary Socialists and the National and Colonial Question, </b>and <i>SW's</i> Lee Sustar leads a session on <b>The Economics of U.S. Imperialism.</b><p>
Courses like <b>Women's Liberation and Socialism </b>will take up the question of oppression under capitalism--where it comes from and how to end it. Sharon Smith, a columnist for <i>SW </i>and author of the new book <i>Women and Socialism, </i>will speak on <b>Abortion Rights: Resisting the Long Retreat. </b>Rebecca Anshell and Kyla Klein answer the question <b>Can Socialism Liberate Women? </b>and Leia Petty and Jessie Kindig  will speak on <b>Alexandra Kollontai: Sexual Relations and the Class Struggle.</b><p>
There is probably no concept in the socialist tradition more controversial, misunderstood and slandered than the revolutionary "vanguard" party of socialists. The idea's of  the Russian revolutionary leader Lenin and the lessons of the 1917 Russian Revolution will be taken up in a course on <b>Lenin and the Russian Revolutionary Movement.</b> A session on <b>Lenin and the Birth of Bolshevism </b>will be led Aaron Hess and Sarah Hines. Jason Yanowitz will present a session titled <b>In Defense of Lenin</b>.<p>
And that's just the beginning of what you'll learn about at Socialism 2005. We invite all of our readers to join us in Chicago!

