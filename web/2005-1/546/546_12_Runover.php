<font face="Times New Roman, Times, serif" size="5"><b>Protesters run over by bigot in LA</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Bruce Cooley</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 3, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">A NOTORIOUS anti-immigrant bigot drove his vehicle through a group of protesters in southern California, hitting at least six people and sending one to the hospital.<p>
The assault took place during a May 25 picket against the Minutemen Project. When activists learned that Jim Gilchrist, founder of the Minutemen, planned to appear at a meeting of the anti-immigrant California Coalition for Immigration Reform to recruit more support for his plan to station vigilante border patrols in Southern California this summer, they came together to organize a protest.<p>
Alarmed by the prospect of a repeat of the armed patrols along Arizona's border in April, more than 300 antiracist and pro-immigrant rights activists--some from as far away as San Diego--turned out for the event in Garden Grove, Calif., in Orange County.<p>
Though five protesters were arrested during the protest and charged with felonies, police released Hal Netkin, the racist who drove through the picket, without charging him with any crime. Activists are demanding that police drop the charges against protesters--and prosecute Netkin for assault. They're also seeking an independent investigation into misconduct by the Garden Grove police.<p>
Yasser Giron, the picketer who was hospitalized, said he recognized that Netkin was about to drive through the picket and was trying to move protesters out of the way when he was struck. Giron suffered injuries to his face, arm and leg.<p>
"It's important to realize that violent racism grows precisely when it goes unopposed," said Giron. "The point of confronting groups like the Minutemen and resisting politicians' attacks is to show them for what they are. They receive political cover from both Democrats and Republicans and have been trying to appear legitimate. They need to be confronted now."<p>
Despite his injuries, Giron thinks that public opposition to anti-immigrant vigilantes must continue. "Demonstrations and pickets are an effective way of confronting these groups," he said. "Not only do they bring attention to this type of hate, but they also show these racists that they cannot go anywhere unopposed. It was beautiful to see activists and community members unified on this issue, against racism."<p>

