<font face="Times New Roman, Times, serif" size="4">Columbia U. plans expansion of its campus</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The invasion of Harlem</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jonah Birch</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 3, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">"My name is Mary Watkins, I've lived in Manhattanville for 45 years, and I don't plan on leaving." "My name is Carmen Ortiz, I live in Manhattanville, and I want to tell Columbia, 'No Way!'"<p>
With these words, residents of a West Harlem neighborhood sent a message to one of the richest schools in the country that they won't give up their homes without a fight.<p>
More than 100 residents joined Columbia University students for an April protest on campus against the school's planned expansion into Harlem. Columbia, which pays no property taxes, although it is the third-largest landholder in New York City, hopes to build a new 17-acre campus between 125th and 133th Streets, west of Broadway--an area known as Manhattanville. <p>
"We want to keep our homes, and we want to keep our neighborhood intact," said Nellie Bailey of the Harlem Tenants Council." Bailey pointed out that increasing rents and cutbacks in government housing programs are making it impossible for all but the wealthiest New Yorkers to live in Manhattan. "Everyone is under the gun if you're poor or working-class in this city," she said.<p>
Columbia has a long and sordid history in Harlem. In 1968, the school's plans to build a private gym in a public park accessible to community residents only through a back entrance--known at the time as "gym crow"--helped spark a massive student rebellion. <p>
The current administration, led by President Lee Bollinger, insists that the school has changed its ways and will keep community interests in mind during the latest expansion. Yet the <i>Spectator, </i>Columbia's student newspaper, has reported that the administration sent a letter to New York's Empire State Development Corporation (ESDC), asking officials to consider using "eminent domain" laws to force unwilling business owners in Manhattanville to turn their property over to the university. Columbia--which had repeatedly promised that eminent domain would not be part of the new expansion--offered to pay the ESDC $300,000 to cover the costs of investigating the status of Manhattanville property.<p>
The discovery of Columbia's shady dealings with the ESDC further infuriated community residents. Activists say that Columbia's expansion would replace dozens of skilled jobs with unskilled work--in a city where nearly half of African American men are unemployed. The new campus would further the process of gentrification in Harlem, which is already becoming unaffordable for many long-time residents. <p>
Columbia has an endowment of more than $4 billion and many connections to city and state politicians--Bailey calls the university "the real welfare queen." Fighting the expansion plans will be a long and difficult task. <p>
But student and faculty support for the Coalition to Preserve Community, which cosponsored the April rally with campus activists, is encouraging. Already, Columbia Professor Robin Kelley--who was born and raised in West Harlem--has been able to get 54 faculty members to sign onto a letter expressing support for the community's fight. These connections are key in building the kind of movement that can challenge Columbia's plans for taking over Harlem.<p>

