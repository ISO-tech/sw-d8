<font face="Times New Roman, Times, serif" size="5"><b>A plan to ban birth control in Wisconsin </b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 3, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">IF ANTI-ABORTION politicians get their way, women will lose access to emergency birth control from the University of Wisconsin (UW) health care system. Last week, Republican State Rep. Daniel LeMahieu introduced legislation that would bar UW health centers from prescribing emergency contraception, calling it "chemical abortion."<p>
Emergency contraception, or the "morning-after" pill, is actually a high dosage of birth control that reduces a woman's chance of becoming pregnant within 72 hours of intercourse, by preventing ovulation or fertilization and interfering with implantation of a fertilized egg into the uterus. Women usually seek emergency contraception when their first form of birth control fails or if they are victims of rape.<p>
The truth is that LeMahieu and his supporters would like to get rid of women's right to birth control completely. When the arch-conservative originally planned his bill, he wanted to ban <i>all </i>forms of prescription birth control--since, he says, it causes women to be "promiscuous." He settled on banning just emergency contraception.<p>
But LeMahieu may get what he wanted anyway, since the legislation--called the UW Birth Control Ban--is so vaguely worded that, if passed, it could ban all oral contraceptives.<p>
This is a familiar scenario in Wisconsin. In 1998, a vaguely worded ban on a late-term abortion procedure misnamed "partial-birth" abortion effectively banned <i>all </i>abortion procedures for a time, until a court intervened.<p>
Last month, about 30 Madison pro-choice activists picketed hearings on the proposed ban and then joined testimony against it. "Most of the women who needed [emergency contraception] were embarrassed and scared," Dr. Beth Wiedel, a resident at UW Hospital, told committee members, who will decide whether the bill goes to a vote in the state assembly. "Withholding this treatment, especially when we have the resources to safely and effectively prevent an unintended pregnancy, is not only unfair, but also highly unethical."<p>
As Madison activist Terry Schmidt explained, "There will be more hearings on this bill before it comes to a vote. Our goal is to build up a more solid base to our group that is fighting this. There is vicious right-wing attack against a woman's right to control her own body. We have to counter these anti-abortion bigots with unapologetic protest wherever they attempt to confront our rights."<p>
Wisconsin is also one of a dozen states considering laws that would permit pharmacists to refuse filling birth control prescriptions on religious grounds (four states already have such a law). The proposal was introduced in Wisconsin after several high-profile cases in which pharmacists were reprimanded for refusing to dispense birth control. <p>
In January, a mother of six sought emergency birth control at a Milwaukee Walgreens pharmacy after a condom broke during sex. The pharmacist, Michelle Long, cited religious beliefs for refusing to fill the prescription--and called the woman a "baby killer." The woman, unable to prevent the unwanted pregnancy, later had an abortion.<p>
Politicians say they want to pass this bill in the name of fighting religious discrimination. But it is women who are being discriminated against. A woman's reproductive choices should be hers--and hers alone.<p>
State by state, the anti-choice fanatics are chipping away at our rights--and they're getting help from Democrats who claim to be supporters of women's rights. In April, Sen. John Kerry (D-Mass.) announced that he was reintroducing the Workplace Religious Freedom Act, cosponsored by the anti-abortion bigot Sen. Rick Santorum (R-Pa.).<p>
Last week, House Republicans thumbed their noses at women serving in the military when they blocked a piece of legislation that would have made emergency contraception available to women soldiers--and another that would allow them to obtain federally funding for abortion in the case of rape or incest. The U.S. Supreme Court announced that in its next term, it will hear a case of parental consent legislation in New Hampshire--the first abortion case to reach the Supreme Court in five years.<p>
The right wing is confident in their goal of wiping out our reproductive freedom. We have to stand up now to oppose them wherever they try to take away our rights.<p>

