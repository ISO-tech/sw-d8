<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">June 3, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>University of California</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Martin Smith, CUE Local 10, and Rebecca Anshell, CUE Local 5</b></font> <p>

<font face="Times New Roman, Times, serif" size="3">ANOTHER SYSTEM-wide strike idled the University of California (UC) after a successful work stoppage barely a month ago by custodians, bus drivers and food service workers represented by AFSCME.<p>
The University Professional and Technical Employees (UPTE)-Communication Workers of America Local 9119, which represents several thousand UC computer, electronics and lab workers, held a one-day strike at eight campuses on May 26.  <p>
Now recognized across the board as one of the harshest employers in the California system, UC has refused to give even minimal concessions to its research and professional employees. Wages for workers represented by UPTE have fallen in real terms by 30 percent during the last 15 years. Simultaneously, the UC system has made massive profits each year that are funneled into pay for wealthy executives of the university, some of whom make over $500,000 a year.  <p>
Workers have been without a contract since September and have been negotiating with the university for about a year. "We've lost our patience with the bad-faith bargaining by UC, and this strike is about unfair labor practices," said UPTE member Janet Clegg.  <p>
Clegg said the UC system is not sending people to the bargaining table who can make decisions. In addition, the union said that UC has not responded to its request for information about how much money is saved through staff turnover. The union is seeking cost-of-living increases and a return to a wage structure that calls for step increases based on job type. <p>
With a strong turnout by its members, the Coalition of University Employees (CUE), which represents clericals, day care workers, and dispatchers at UC, honored UPTE's picket lines and authorized strike pay, showing the solidarity that is needed to rebuild the labor movement.  <p>
At UC-San Diego, about 200 workers from UPTE as well as other unions and students walked pickets at key locations on campus, chanting "Hey, hey, ho, ho, UC greed has got to go!"  <p>
CUE is currently in the process of a strike vote as well and may call for a work stoppage as early as next week in what would be the third strike at UC in two months.<p>
Numerous AFSCME workers and students came out to show their support as well, and at UC-Santa Cruz, a vibrant student organization--the Student and Worker Coalition for Justice (SWCJ)--called on faculty to cancel or move classes and rallied fellow students to get involved in the fight for labor justice.  <p>
Though UC union members have shown solidarity by joining each other's picket lines, the best way to force UC to bargain in good faith is for the various campus unions to hold simultaneous work stoppages and build solidarity with students. Sadly, UPTE leaders told the SWCJ that they would call the police if SWCJ attempted a repeat of its 500-strong sit-in that blocked traffic during the AFSCME strike in April.<p>
In fact, these sorts of actions and more coordination among unions will be key to winning workers' demands for fair contracts.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Southern Connecticut State University</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Max Clark </b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW HAVEN, Conn.--About 75 union members and supporters picketed graduation ceremonies at Southern Connecticut State University (SCSU) to support UNITE HERE Local 217's contract fight with Chartwells College &amp; University Dining Services. Members of UNITE HERE Local 34 service and maintenance workers, GESO graduate teachers, District 1199 hospital workers and the International Socialist Organization attended in solidarity.  <p>
The Compass Group, of which Chartwells is a subsidiary, raked in nearly $22 billion last year. Local 217 recently voted to authorize strike actions against the company by a 58-to-5 margin. Negotiations broke down after a "last and best" offer from the company, which may prove to be neither. <p>
Simply put, Chartwells isn't willing to compromise anything except the lives of their employees. Now, health care, pensions, work rules and the grievance system are being taken off the bargaining table as unworkable compromises and onto the streets as demands.<p>

