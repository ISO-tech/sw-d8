<font face="Times New Roman, Times, serif" size="5"><b>Bush threatens to veto stem cell bill</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 3, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">HOW RABID is the Republican Party's right wing? So rabid that it might torpedo a bill supported and cosponsored by moderate Republicans.<p>
Last week, the U.S. House of Representatives voted 238-194 to approve a bill that would lift a 2001 ban on allowing taxpayer money to underwrite research using stem cells taken from the tens of thousands of unclaimed embryos that are discarded by fertility clinics each year.<p>
Stem cells, if they come from embryonic tissue in the first few days of development, have the potential to be transformed into any of the body's dozens of different cell types. Scientists say they could be used to potentially develop treatments for debilitating illnesses that affect millions of living human beings each year, like Alzheimer's, Parkinson's and spinal cord injuries.<p>
Just 22 stem cell lines are currently available for federally funded research--and all have been grown in laboratory dishes on layers of mouse cells, making them poor candidates for use in human experiments.<p>
According to a recent poll by the Pew Research Center for the People and the Press, a majority of Americans favor stem cell research. Even among self-described social conservatives, 40 percent favored conducting such research.<p>
But George W. Bush and the anti-choice fanatics are out to kill the legislation that 50 moderate Republican House members voted for.<p>
"This bill would take us across a critical ethical line by creating new incentives for the ongoing destruction of emerging human life," Bush said last week, threatening to veto the legislation if it makes it through the Senate. This, from the man who bombed millions of innocent Iraqis in his war for oil and empire.<p>
But the award for the biggest hypocrite has to go to Sen. Sam Brownback (R-Kan.). After weeks of Republican complaints about Democrats' "irresponsible" use of the filibuster against Bush's judicial nominees, Brownback implied on ABC's <i>This Week </i>that <i>he </i>might filibuster the stem cell measure if it comes up in the Senate.<p>
Actually, even more scorn should be heaped on the 14 House Democrats who broke ranks to vote <i>against</i> the stem cell bill--some of whom cited concerns about the potential for opening the door to things like human cloning.<p>
In a sane society, research that could improve the quality of life for millions wouldn't be held up by these fanatics.

