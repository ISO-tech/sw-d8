<font face="Arial, Helvetica, sans-serif" size="2"><b>VIEWS AND VOICES</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Labor and antiwar activists are stronger together</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Power in their unity"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 3, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">IN AN unprecedented display of solidarity between the antiwar and labor movements, members of Southern Connecticut State University (SCSU) No War and UNITE HERE Local 217 picketed and marched April 27 to demand a fair contract for the workers and an end to the war in Iraq. <p>
SCSU No War had organized a day of protest on campus because Army recruiters were scheduled to table all day, and former Secretary of State Madeleine Albright had a high-profile speaking event that evening. <p>
Albright was responsible for many horrific foreign policy initiatives in the Clinton administration, overseeing the disastrous economic sanctions on Iraq that killed more than 1 million Iraqis. SCSU No War declared that it would not accept a war criminal such as Albright on campus.<p>
The day got off to a positive start when the Army recruiters left campus after only 90 minutes, not talking to a single interested student. Antiwar activists, meanwhile, passed out pamphlets and fliers that slammed the war and argued with students to come out to protest Albright that evening.<p>
During the counter-recruitment action, activists learned that Local 217, which represents the 70 dining hall workers at SCSU, would be holding an informational picket in front of the dining hall at the same time we were planning on convening to march to the Albright protest. <p>
The workers, who work for Chartwells, a multinational corporation that runs the food service, have been working without a contract since it expired in February. Workers complain of a lack of respect on the job, and those active in the union have been threatened, harassed and even fired outright for the smallest infractions. <p>
Students in the antiwar coalition quickly decided to move the meeting spot for the march to the front of the dining hall in order to show solidarity with the workers who are battling the war at home. As Rich, a member of the antiwar coalition, said, "The same people in corporate boardrooms are making profits by waging war in Iraq and slashing our wages here."<p>
More than 40 workers and students joined the picket line, which was well received by workers inside and students passing by. The workers immediately took up antiwar chants and alternated them with their union chants. <p>
After 45 minutes in front of the dining hall, all of the workers and students marched behind the antiwar coalition's banner to protest Albright's speaking event, where they were joined by a handful of community members and supporters. Although SCSU claims there is no money for the dining hall workers' contract, they forked over more than $70,000 to Albright for her two-hour appearance, according to the <i>Southern News</i>. <p>
As Bransley Barnaby, an activist in Local 217 who has worked at SCSU for 14 years, said at the event, "This solidarity is incredible. We're here for the same reasons--to combat corporate greed. They're stealing our health care and insurance the same way they're stealing it from soldiers. Those soldiers just want to find a way to take care of their families--same as us and the students who go here. It's great that we could all join in together--unions and antiwar, workers and students. This is an amazing example of people using their power in unity so that we can all get out of the same mess, whether it's in Baghdad or New Haven."<br>
<b>Sam Bernstein,</b> New Haven, Conn.<p>

