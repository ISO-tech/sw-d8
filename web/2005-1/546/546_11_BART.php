<font face="Times New Roman, Times, serif" size="4">Managers get raises and ask workers to sacrifice</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>BART workers prepare for contract showdown</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lucas Nevarez</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 3, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--With a contract that expires in less than six weeks, BART workers are facing a mounting attack from management in order to eliminate a $53 million deficit. <p>
Labor negotiations have hit major roadblocks. Management's solution to the money issues means another round of layoffs, asking workers to accept a "wage freeze" and increasing out-of-pocket charges for health care and retirement. In addition, BART officials want to impose an across-the-board fare increase. Union negotiator Larry Hendel described management's proposal as "the worst in the history of BART."<p>
Union members are not taking these attacks lying down. On May 18, BART employees took a step forward in preparation for what might turn out to be a repeat of the 2001 BART strike. Workers spent the day handing out thousands of flyers at many stations in San Francisco and around the Bay Area--a sign that workers may be preparing for a repeat of the 2001 BART strike. The flyers contained facts about the dispute and asked the public to get involved by putting pressure on BART management. <p>
Management says its financial problems are the result of labor agreements, but this is simply not true. BART workers are facing pay cuts, continued layoffs and major concessions on health care and retirement while managers are grossly overpaid.<p>
"A lot of the problems stem from the layers and layers of bureaucracy around this place," one union member told <i>Socialist Worker.</i> "There are hundreds of managers and executives that make more than $100,000 a year...What's really bad is that BART general manager [Tom Margro] makes more than $260,000 a year!" The reality is that Margro is set to receive a 5 percent wage <i>increase </i>and a $19,600 bonus at the same time that he's demanding sacrifice from workers. <p>
This hypocrisy has to end. A strike may be the only way to stop the attacks, and for this labor struggle to turn around.<p>
<i>For more information about the labor dispute, visit <a href="http://www.bartriders.com" target="_blank">www.bartriders.com</a></i>.

