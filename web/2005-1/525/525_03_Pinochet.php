<font face="Times New Roman, Times, serif" size="5"><b>The crimes of a U.S.-backed dictator </b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 7, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">IF THE ex-dictator of Chile, Augusto Pinochet, is finally put on trial for his crimes, his protector, Henry Kissinger, the former U.S. Secretary of State, should be standing alongside him.<p>
Last month's indictment of Pinochet, who led the military coup that overthrew the democratically elected government of Chilean President Salvador Allende in 1973, could lead to a trial for some of the estimated 45,000 political killings committed under his rule.<p>
Kissinger, as declassified U.S. government documents show, orchestrated efforts to destabilize Allende's government, which was made up of a coalition of socialist parties. "I don't see why we need to stand idly by and let a country go communist due to the irresponsibility of its own people," he said after Allende was elected. <p>
Latin American solidarity activists and authors Roger Burbach and Paul Cantor described the impact of the U.S.-backed coup: "The military junta Pinochet led dissolved Congress, outlawed political parties and the largest labor union in the country, censored the press, banned the movie <i>Fiddler on the Roof</i> as Marxist propaganda, publicly burned books...expelled students and professors from universities, designated military officers as university rectors, and arrested, tortured and killed thousands who opposed the regime."<p>
Pinochet was also responsible for Operation Condor--a CIA-approved assassination spree against left-wing oppositionists across South America. Washington's response was to give Pinochet "a $3 million payment stemming from official duties involving the United States," the <i>New York Times </i>reported last month.<p>
Pinochet retired in 1990. The first attempt to prosecute him, in 1998, fizzled as the old butcher pretended to be medically unfit to stand trial. In recent months, however, Pinochet has publicly defended his brutality--and popular pressure forced prosecutors to pursue him once more.<p>
If Pinochet felt safe enough to boast, it's because he took confidence from Washington's increasingly aggressive policy in Latin America--which includes the militarization of the Andes region through Plan Colombia, as well as U.S. support for the failed coup against Venezuelan President Hugo Ch&aacute;vez.<p>
Washington is determined to draw the line against social movements and popular rebellions that have ousted governments in several Latin American countries. Thus, in November, Defense Secretary Donald Rumsfeld used a meeting of Western Hemisphere defense ministers to call for multinational coordination of armed forces in the Americas to fight "terrorism"--a revival of the joint military structures used in Pinochet-era "dirty wars" against the left.<p>
If Pinochet is put on trial, we should use this opportunity not only to expose past U.S.-sponsored crimes, but to organize against present and future atrocities as well.<p>

