<font face="Times New Roman, Times, serif" size="5"><b>I am at my lowest low</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 7, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
I am a single woman, currently unemployed, with a home-based business that is barely bringing in any income. I have always supported myself by working in nightclub industries as a promoter and makeup artist, and writing resumes (my business). <p>
In September, when I was about five-and-a-half months pregnant, I could no longer work in the nightclub industry, which was 90 percent of my income and allowed me to afford my rent and pay most of my bills. I tried to look for a job, but a lot of places would not hire me because of my pregnancy. <p>
I fell three months behind on my rent--about $3,300. I have lived in my apartment for seven-and-a-half years and have always paid the rent. My landlord recently gave me an eviction letter asking me to come up with the money or leave in three days. I wrote a letter explaining that I was due to give birth and, as of January 1, would be back to work and caught up. <p>
I explained and apologized for any inconvenience that I may have caused. But he and his lawyer went ahead and started the eviction process.<p>
I am at my lowest low. I cry every night because I have no support. The social service people said they will not provide any rental assistance because my rent is too high. But my rent is standard for a New York City apartment in a decent neighborhood. <p>
I just can't believe that in this country, there are people made homeless everyday, and that the law will allow a landlord or a judge or a lawyer to evict a pregnant woman and contribute to homelessness. <p>
I could understand if I was working and refusing to pay rent. Then I would think that was the right action to take on his part. But I am showing an effort to pay, and I have a good reason. <p>
It is sad that we live in a society where cash comes before being human. It is sad that we live in society that contributes to homelessness because someone has suffered a financial crisis or major setback.<p>
I have called every charity in my area. Most of them tell me they have no funds or don't provide funds. I called a lawyer to assist me in court. I explained that I am getting evicted, and all he could say to me is "I could represent you for $600." I just told this man that I am being evicted--that means I have no money. <p>
I was not able to afford an attorney, and the landlord got the upper hand. I was given the "choice" of coming up with almost $6,000 by February 28--that will include January and February rent and the back rent I owe. I have to start making a payment in the next 20 days of $500. If I am a day late, they will send the marshal to evict me. So, I am basically still in the same position. <p>
I have been to the charities that I was told to go to. A lot of them say there is no money. The Salvation Army said they could only give me $600, but that is only if I could show them proof that I can come up with the rest. I tried to get a one-shot deal, but they said I needed to have a job to qualify to show how I will pay them back. If I had money and a job I would not be going to them! They made no sense.<p>
We have billions to use for war, but no help or funds for individuals in need. It is sad that we call ourselves the richest country in the world. We are the poorest, because of our poor sense of humanity.<br>
<b>Anonymous, </b>New York City<p>
