<font face="Times New Roman, Times, serif" size="5"><b>Show soldiers we support their resistance</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 7, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
In a blow to imperial hierarchy, soldiers of the 343rd Quartermaster Company serving as members of a convoy to deliver supplies to the front line in Iraq refused to take on what they called a "suicide mission." The Iraqi resistance has routinely targeted convoys, and the soldiers claimed that they were not properly protected. <p>
The Army said that the soldiers weren't being detained for their "temporary breakdown" of discipline, only being questioned. But families of the soldiers involved said the opposite. <p>
In a phone call, Amber McClenny, a soldier involved, told her mother "This is a real, real, big emergency...I need you to contact someone. I mean, raise pure hell...We had broken-down trucks, non-armored vehicles and, um, we were carrying contaminated fuel. They are holding us against our will. We are now prisoners."<p>
There were three things that brought the U.S. down in the Vietnam War. One was the resistance at home, second was the resistance in Vietnam, and the final component was the resistance of U.S. soldiers. <p>
The current Iraq war has a strong resistance in Iraq, and there may be a spark from soldiers. Now, the only problem is that the election year demobilized the antiwar resistance at home. Instead of having a national protest to demand the release of these soldiers, there was no word from the antiwar movement. <p>
We cannot rely on politicians in Washington to have a good heart and get out of Iraq. We need to support these soldiers to encourage more soldier resistance. Soldiers who are thinking about refusing orders need to know that we support soldier resistance. <p>
It is time for us at home to "Raise pure hell"!<br>
<b>Matias Marin, </b>Riverside, Calif.<p>

