<font face="Times New Roman, Times, serif" size="5"><b>Exposing war crimes in Guant&aacute;namo</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | Janury 7, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Michael Ratner and Ellen Ray, <b><i>Guant&aacute;namo: What the World Should Know. </b></i>Chelsea Green Publishing, 2004, 159 pages, $15.</font><p>

<font face="Times New Roman, Times, serif" size="3">SINCE SEPTEMBER 11, the Bush administration has operated under the assumption that civil liberties are expendable. Nowhere is this as apparent as at the U.S. prison camp in Guant&aacute;namo Bay, Cuba, where hundreds of prisoners rounded up during the wars in Afghanistan and Iraq continue to languish--some, three years after their initial arrest.<p>
<i>Guant&aacute;namo: What the World Should Know</i>, a recent book by civil rights expert Michael Ratner and journalist Ellen Ray, is a useful primer for those interested in not only the situation of the prisoners at Guant&aacute;namo, but the legal implications of the Bush administration's continuing attack on civil liberties.<p>
Done in an interview format, Ratner, the president of the Center for Constitutional Rights, one of the main organizations that has handled legal challenges on behalf of the Guant&aacute;namo prisoners, explains in detail the horrific conditions at the camp that have led to dozens of suicide attempts.<p>
"Guant&aacute;namo is like Dante's ninth circle of hell," says Ratner. "The temperature is often 110 degrees Fahrenheit, and of course the prisoners have no such thing as air conditioning. The place is infested by scorpions and banana rats. The detainees sleep on concrete floors, with no mattresses; the toilet is a hole in the ground."<p>
Ratner provides a rundown of the legal issues involved in high-profile cases, such as those of U.S. citizens Yaser Hamdi and Jose Padilla, and chillingly details how the sickening mental and physical abuse of prisoners at Abu Ghraib prison in Iraq can be traced to Guant&aacute;namo as well.<p>
One of the documents included in the book's useful appendix is a memo to the president from White House legal counsel Alberto Gonzales making the case for why the "war on terror" renders the Geneva Convention "obsolete." In particular, Gonzales expresses concern that the Geneva Convention could be used to prosecute U.S. soldiers for war crimes or "outrages against personal dignity" committed against prisoners.<p>
As Ratner points out, this memo helped give legal justification to the kind of horrific abuses that have occurred at Abu Ghraib prison in Iraq, at Guant&aacute;namo Bay, and elsewhere around the globe since September 11. Of course, Gonzales' name is once again in the news--as Bush's new pick to replace John Ashcroft as Attorney General.<p>
Readers looking for up-to-date information on Guant&aacute;namo will have to do some supplemental research, as the book went to press prior to important legal developments--including a recent federal court ruling that declared the military tribunal process to be illegal under both the Constitution and the Geneva Convention.<p>
However, this slim volume paints a stunning portrait of the arrogance of the Bush administration as it continues to roll over our rights--and is a valuable resource for those interested in learning more about the scope and impact of the Bush administration's attacks. As Ratner concludes, "We are at the beginning of what will be a long struggle to repair the damage that the government has inflicted on us all."<p>

