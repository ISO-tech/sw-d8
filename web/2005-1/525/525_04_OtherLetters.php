<p><a name="University"></a><font face="Times New Roman, Times, serif" size="4"><b>Taken over by a university</b></font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
Most of the oppressed workers I know work in food service here in a West Virginia college town of 25,000 spoiled rich-kid students who like to drink, party and dine out. The workers are indigenous people, stuck in minimum-wage and part-time jobs with no benefits, at privately owned businesses, restaurants and bars.<p>
What can they do? My sons are two of these workers. They feel trapped. They work to survive--and barely at that. There is no industry here (unless you count the piggy capitalist students with their deep pocket parental-financed appetites).<p>
The university is taking over the town and are tearing down houses. Ours is slated for demolition--we have no control because we rent. They are catering to the wealthier students, building <i>them</i> fancy upscale apartments, while we will be relegated to the slum areas, forced to walk, dodging drunken-driver students wielding cell phones, and so on.<p>
Is this shades of the <i>Communist Manifesto</i>?<br>
<b>Anonymous, </b>from the Internet<p>
