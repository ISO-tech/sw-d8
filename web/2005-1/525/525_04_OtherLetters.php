<p><a name="HRC"></a><font face="Times New Roman, Times, serif" size="4"><b>HRC's agenda is rock solid</b></font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
Regarding "Liberal groups that shill for Democrats" (<i>SW</i>, December 17), the Human Rights Campaign (HRC) was never contacted regarding a piece in the <i>New York Times </i>that we responded to as being an incomplete and inaccurate article. We do not support the privatization of Social Security and would never undermine the rights of any other group to secure gay, lesbian, bisexual and transgender (GLBT) rights. <p>
In no way are we retreating from our rock-solid commitment to full marriage equality. Our only shift is to make the stories of real GLBT Americans and the protections we're denied under law more prominent. <p>
Bringing real lives into the fold of any discussion shaping the laws of this nation is something I would assume most of your readers would favor.<br>
<b>J. Smith, </b>HRC Senior Communications Manager, Washington, D.C.<p>
