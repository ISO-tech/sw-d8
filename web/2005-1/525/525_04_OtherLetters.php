<font face="Times New Roman, Times, serif" size="4"><b>The terrorist who runs Iraq</b></font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
Iyad Allawi, who was appointed prime minister of Iraq under the terms of the June 2004 constitution imposed by the occupying powers, is a well-known terrorist. One of his bombs killed schoolchildren on a bus. He is also accused of murdering six blindfolded and handcuffed prisoners at the infamous al-Amariyah security center in Baghdad last June.<p>
George W. Bush's appointee for prime minister appears to enjoy murdering shackled prisoners and children for sport.<p>
Allawi is a former member of Saddam Hussein's Baath Party. He quit or was expelled in 1975--an event that the occupying armies find impressive.<p>
Allawi next became a CIA operative, informing on friends who later disappeared. He is credited with numerous bombings while employed by the U.S. taxpayers. These include a movie theater full of people and a mosque where Muslims worshipped. A bus bombing was timed to explode when children normally used it to get to school.<p>
The Bush administration protests that Allawi is not a terrorist. The cinema, mosque and school children were blown up for "our side," so Allawi is a "hero."<p>
Allawi's visits to al-Amariyah are considered very beneficial to George Bush's battle against "evildoers." The prime minister's assistance in the interrogation of Iraqis who were being tortured could generate more names of those who object to the occupation. Killing shackled prisoners while those who will be interrogated next watch often helps to loosen their tongues.<p>
A prime minister who enjoys getting into the thick of things is a rare find, even for Bush.<br>
<b>Richard Geffken, </b>Sumter Correction Institution, Bushnell, Fla.<p>
