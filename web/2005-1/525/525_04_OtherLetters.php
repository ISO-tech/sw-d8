<p><a name="Protest"></a><font face="Times New Roman, Times, serif" size="4"><b>I'll be there to protest Bush</b></font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
It is clear that the Bush administration will do everything to keep protesters away from the inauguration parade. They have succeeded almost every time in the past. I participated in protests in Washington, D.C., and New York City that went virtually unnoticed, regardless of the numbers involved (more than 500,000 at the Republican National Convention).<p>
Regardless, I will be in D.C. once again just to turn my back on Bush. This time, to get through security and up close, I will wear a Bush pin, a U.S. flag pin, a "support our troops" pin--or all three. Bush cheated me out of a country, so my cheat would be quite minor in comparison.<p>
I hope you can suggest similar ideas to your members to get through security and make this protest as effective as possible. We owe it to ourselves and the world. <br>
<b>Chuck Lance, </b>From the Internet<p>

