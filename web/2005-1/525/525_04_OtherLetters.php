Since I witnessed the conviction of my stepdaughter for a murder that I know she had absolutely nothing to do with, I find these shows less than entertaining. For conformation of this fact, read "Junk Forensics in San Diego, the Cheri Lynn Dale Story" on the Web.<br>
<b>Charles Caldwell, </b>Leucadia, Calif.<p>
