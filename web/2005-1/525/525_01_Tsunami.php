<font face="Times New Roman, Times, serif" size="4">Tsunami horror in Asia:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why isn't Bush doing more?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 7, 2005 | Pages 1 and 2</font><p>

<font face="Times New Roman, Times, serif" size="3">MOST PEOPLE reacted with horror to the heartbreaking images of the tsunami disaster that struck a dozen countries ringing the Indian Ocean on December 26. But not George W. Bush.<p>

