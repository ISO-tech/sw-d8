His first statement on the disaster--a full 72 hours after the tsunamis struck--was dominated not by sympathy for the victims, but his venting at a United Nations (UN) official, Jan Egeland, who had dared to state the indisputable fact that rich nations are "stingy" in providing foreign aid.<p>
Then, without meaning to, Bush made Egeland's point for him. After sputtering that "the person who made that statement [about rich nations being stingy] was very misguided and ill-informed," Bush announced that the richest nation in the world would contribute all of $35 million in aid.<p>
The survivors of the worst natural disaster in a generation were still finding corpses--and wondering if help would ever come. And the president of the world's lone superpower was promising the equivalent of three-and-a-half hours' spending in his war on the Iraqi people.<p>
Even the gutless U.S. media noticed the gap between Bush's pledges of help and the scale of the catastrophe.<p>
Estimates of the death toll seemed to double each day, reaching 150,000 by early January. But the worst could be yet to come. The disaster left at least 5 million people scrambling for food and clean water, and threatened by epidemics, according to UN officials. "This isn't just a situation of giving out food and water," said Rod Volway, of CARE Canada, one of the first aid groups to arrive in Aceh, the northwestern province of Indonesia that was hit first and hardest. "Entire towns and villages need to be rebuilt from the ground up. Everything has been destroyed."<p>
By instinct, the U.S. media immediately focused on one small subsection of the victims--Europeans and a tiny number of Americans who were vacationing in beach resorts in southern Thailand and elsewhere. But the vast majority of victims lived in the unknown fishing villages and poverty-stricken coastal towns that dot the shores of the Indian Ocean--from Indonesia, to India and Sri Lanka, to the African country of Somalia. And long after the tourists had been taken to hospitals or transported home, the poorest of the poor had yet to see any relief.<p>
One man in Aceh described the desperate scramble when supplies finally reached one isolated village. "The fastest get the food, the strong one wins," he told Reuters. "We feel like dogs."<p>
The suffering caused by the tsunamis has been met by an almost unprecedented outpouring of charity from ordinary people. According to the <i>Boston Globe, </i>in just five days following the disaster, the American Red Cross raised nearly $50 million to help provide water, food, shelter, medicine and other supplies to survivors, and nine other U.S. charities matched that sum.<p>
This generosity is in stark contrast to Bush's attitude. In the first several days, the White House barely acknowledged the nightmare, and Bush couldn't stir himself from his "post-Christmas" vacation to speak up.<p>
Eventually, the administration was shamed into raising its promise of disaster aid from $15 million to $35 million, and then to $350 million.<p>
But there's no guarantee that the money will ever get to Asia. When a devastating earthquake struck Iran one year to the day before the tsunami disaster, the Bush administration promised a token amount of aid. That money, the <i>New York Times </i>pointed out last week, "still has not been delivered."<p>
Even if the U.S. follows through on its pledges this time, $350 million is far too low. <p>
America's two corporate-dominated parties, the Republicans and Democrats, spent nearly that much staging their flag-waving conventions last summer. Just two of the Air Force's new generation of killing machines--the F/A-22 Raptor fighter jet--could cover the entire cost with plenty to spare. America's richest multi-billionaires could have the whole sum withdrawn from their bank accounts, and they would barely notice.<p>
Bush claimed that the U.S. would eventually provide as much as $1 billion to countries hit by the disaster. But even that is about 0.5 percent of what the White House plans to spend for the war on Iraq.<p>
Now, Secretary of State Colin Powell and Florida Gov. Jeb Bush have the assignment of patching up Washington's image at a hastily called international summit in Indonesia this week.<p>
They may well agree to one program that pundits universally scorned as a waste of money in the days after the disaster--an early-warning system for tsunamis, like the one that for 40 years has protected richer countries ringing the Pacific Ocean.<p>
The U.S. may even allow discussion of proposals to ease the crippling debt burden suffered by some of the hardest-hit countries. <p>
But Washington's idea of debt relief doesn't provide much relief. In the 1990s, the U.S. and other powerful governments agreed to a plan to eliminate a portion of debt for the world's poorest countries, but the initiative isn't even halfway to its goal. About half of the poor countries involved still pay more in interest to the International Monetary Fund than they spend on health services.<p>
The victims of last month's unspeakable tragedy deserve better than this. There's no reason that the U.S. government shouldn't mobilize all the massive resources at its command to help those suffering the natural disaster of the tsunamis--and the unnatural disaster of poverty and environmental devastation.<p>

