<font face="Times New Roman, Times, serif" size="5"><b>Newest crop of homeless U.S. vets</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | Janury 7, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">FAMILIAR IMAGES of homeless veterans, scarred by war, may become even more common in the coming months and years. That's because, according to recent reports, there's a new crop of homeless veterans starting to appear on the streets--veterans from the Afghanistan and current Iraq war.<p>
Nearly 300,000 veterans are homeless on any given night--about one-quarter of the entire U.S. homeless population. A large number of these veterans suffer from post-traumatic stress disorder, depression, drug addiction or other combat- and stress-related health problems.<p>
Today, the tens of thousands of U.S. troops who have served in Iraq and Afghanistan are just as vulnerable, and homeless and veterans advocates say they are already beginning to see a first wave of Iraq vets who are winding up on the streets as a result. "When we already have people from Iraq on the streets, my God," Linda Boone, executive director of the National Coalition for Homeless Veterans recently told United Press International (UPI). "I have talked to enough [shelters] to know we are getting them. It is happening and this nation is not prepared for that."<p>
For the soldiers themselves, the shock and anger at being abandoned by the government is overwhelming.<p>
Like Seabees Petty Officer Luis Arellano, who told UPI that he was pushed out of the military after shrapnel nearly took off his left thumb in Iraq. "It was more of a rush," he said. They put us in a warehouse for a while. They treated us like cattle.<br>
It is all about numbers. Instead of getting quality care, they were trying to get everybody demobilized during a certain time frame. If you had a problem, they said, 'Let the [Department of Veterans Affairs] take care of it.'"<p>
When Arellano tried to get help from the Veterans Administration (VA), he found they couldn't take him. "When we got there, the VA was totally full," he said. "They said, 'We'll call you.' But I developed depression."<p>
The depression caused Arellano to leave his job, and he and his wife split up. That's when he ended up homeless, sometimes living out of his truck. "One day you have a home, and the next day you are on the streets," he said.<p>

