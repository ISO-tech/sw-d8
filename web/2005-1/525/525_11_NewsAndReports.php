<font face="Arial, Helvetica, sans-serif" size="2">January 7, 2004 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>No to war and occupation</b></font><p>

<font face="Times New Roman, Times, serif" size="3">PROVIDENCE, R.I.--Despite a frigid winter rain, the Community Coalition for Peace had its first antiwar action on December 13, demonstrating in front of the National Guard recruiting station. That very day, more Rhode Island Guardsmen were being flown out to participate in the occupation of Iraq.<p>
Chanting "Send George Bush, bring the troops home!" peace activists persuaded some passers-by to pick up a sign and join the picket, swelling the ranks to 40 at the height. The protest also garnered some local radio and television coverage.<p>
In <b>Wichita, Kan.,</b> on New Year's Eve, Wichita's Peace and Social Justice Center, along with members of the ISO and one soldier on leave from Iraq, stood outside the McConnell Air Force Base in Wichita in protest of the war in Iraq. Signs carried slogans such as "Bring them home" and "War is big bu$ine$$."<p>
<i>Jane Hamlin and Justin Nicholes contributed to this report.</i> <p>

