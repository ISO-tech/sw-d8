<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">January 7, 2004 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Sacramento grocery workers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Marcia Thorndike, UFCW Local 101 (retiree)</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IN THE last week of December, after five months of negotiations and contract extensions with grocery chains Raley's, Safeway, Albertsons and Ralphs, Sacramento-based United Food and Commercial Workers (UFCW) Local 588 sent out copies of the proposed contract and mail-in ballots to its 17,000 grocery industry members. <p>
Votes will be tallied on January 7, despite the fact that meetings to discuss the contract are scheduled for the following week on January 13. <p>
Local 588 President Jack Loveall hailed the agreement as a victory--but most members think otherwise. "I calculate this contract will cost me roughly $3,000 a year," Gary Gernet, a produce clerk at Safeway for the last 32 years, told <i>Socialist Worker.</i> "Amazingly, they're calling it a victory."<p>
There's also a long list of concessions that are not at all ambiguous, including reductions in Sunday hourly pay, reductions in holiday pay, doubling of fees for doctor visits to $20, introduction of yearly deductibles for medical insurance ranging from $200 to $600 per individual, lowered employer contribution to the health and welfare fund and a "no maintenance of benefits" clause, which means that when the fund money is gone, so are the benefits.<p>
President Loveall proudly boasted that the contract contained "no permanent two-tier system." Retirees, however, are now divided into three categories, with two of them now slated to pay monthly fees for their health benefits. Also, the time it takes to progress to journeyman pay has been tripled. "You could become a doctor in the time it takes to become a journeyman clerk," quipped one of Gernet's coworkers.<p>
The contract specifically forbids Local 588 from cooperating with a consumer boycott of their stores, which could sharply limit their ability to show solidarity with their 35,000 UFCW brothers and sisters negotiating contracts in the Bay Area. Yet given the devastating concessions that Southern California grocery workers agreed to after a four-and-a-half month strike, most Local 588 members feel relief about not striking.<p>
There are, however, also signs of opposition--in the form of a petition being organized by a small but active rank-and-file group called Reform 588. The petition calls on UFCW International President Joseph Hansen to halt the vote on the contract to give members time to discuss and debate the proposal, as he did in Denver's UFCW Local 7.<p>
But this is a long shot, given that Hansen was instrumental in the notorious crusade by the UFCW International to crush the Local P-9 strike in 1985 at a Hormel plant.<p>
Reform is desperately needed in Local 588--which is illustrated by Loveall's recent resignation and the vote of the Local 588 executive board to replace him with his son, Jacques Loveall. In response to member criticisms of the contract, Adam Loveall--another of Jack's sons--brushed them off. "We don't have the luxury of detailing each contract to each member's life," he said. "We got this without going on strike. Anyone can go on strike."<p>
