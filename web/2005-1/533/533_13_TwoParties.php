<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Can we change the Democrats?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 4, 2005 | Page 13</font><p>

<font face="Times New Roman, Times, serif" size="3">ONE OF the greatest obstacles to building a working-class movement for fundamental change in the United States has been the two-party political system.<p>
This was recognized by Frederick Engels as far back as 1893, where he pointed out that the formation of a workers' party in the U.S. was hindered by the "Constitution...which makes it appear as though every vote were lost that is cast for a candidate not put up by one of the two governing parties." This allowed the electoral system to be dominated by two marginally different bourgeois parties, but both intimately tied and committed to capitalism.<p>
Engels described the American political system as consisting of "two great gangs of speculators, who alternately take possession of the state power and exploit it by the most corrupt means and for the most corrupt ends." Both parties were funded by big business; both were supporters of overseas conquest; both used troops to quell strikes; and both upheld Jim Crow segregation.<p>
That didn't stop the Democratic Party from trying to pawn itself off as a party of the people. One of the secrets of the success of the two-party system was the ability of the Democrats to sell themselves, in however limited a way, as an alternative to the "excesses" of the Republican Party. It projected this image as a means of co-opting, and ultimately destroying, any independent political challengers.<p>
Socialist leader Eugene Debs understood the problem well, and in his Socialist Party campaign speeches constantly came back to the theme. "If the Democratic Party is the 'friend of labor' any more than the Republican Party," he said in 1900, "why is its platform dumb in the presence of Coeur d'Alene? [an 1892 strike of Colorado miners that was broken up by sending in 1,500 troops and locking up 600 strikers in cages].<p>
"It knows the truth about these shocking outrages--crimes upon workingmen, their wives and children, which would blacken the pages of Siberia--why does it not speak out? What has the Democratic Party to say about the 'property and educational qualifications' in North Carolina and Louisiana, and the proposed general disfranchisement of the Negro race in the Southern states? The difference between the Republican and Democratic Parties involve no issue, no principle in which the working class have any interest, and whether the spoils be distributed by Hanna and Platt, or by Croker and Tammany Hall is all the same to them."<p>
The first challenge to the two-party system came from populism, which grew into a mass movement in the 1880s and 1890s, mostly among Southern and Western poor farmers. The People's Party, which held its first national convention in 1892, advocated public ownership of the railways, a progressive income tax and free coinage of silver.<p>
Alarmed by the growth of the Populist Party, which received 1.4 million votes in the 1894 mid-term elections, the Democratic Party fielded William Jennings Bryan as its presidential candidate in 1896. Bryan breathed populist rhetoric in order to win over Populist Party supporters. Democratic leaders also diligently cultivated the "fusionist" wing of the People's Party, which proposed merging the two parties.<p>
At its 1896 convention, the Populists voted to endorse Bryan as their candidate. Populist leader Tom Watson knew this signaled the end of populism: "Populists cannot denounce the sins of the two old parties," he warned, "and yet go into political copartnership with them. "The moment we make a treaty the war must cease...and when we cease our war upon the two old parties, we have no longer any excuse for living."<p>
Watson warned that "fusion means the Populist Party will play Jonah, and they [the Democrats] will play the whale." Watson was right. The decision to back Bryan effectively killed the Populist Party.<p>
As our modern Jonahs, in the form of organizations like "Progressive Democrats for America," enter the stomach of the Democratic Party whale, it's worth remembering this history.<p>

