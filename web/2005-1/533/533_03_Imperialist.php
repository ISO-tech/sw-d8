<font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Democrats climb on Bush's foreign policy bandwagon</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Washington's imperialist consensus</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 4, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">YOU KNEW just how right wing the U.S. foreign policy debate had become when both Republican and Democratic politicians started slamming George W. Bush for being weak-kneed during his recent trip to Europe.<p>
Former <i>New York Times </i>columnist William Safire led the conservatives' charge, denouncing Bush for failing to confront Russian President Vladimir Putin's "retreat from democracy" during the two leaders' meeting in Slovakia. When news came days later that Russia planned to sell nuclear fuel to Iran to supply reactors designed to produce electricity, Sen. John McCain (R-Ariz.) raised the stakes, demanding that Russia be excluded from the next meeting of the Group of Eight club of rich countries.<p>
Sen. Joe Lieberman, the Democratic candidate for vice president in 2000, backed McCain. Rep. Jane Harman of California, the top Democrat on the House Intelligence Committee, jumped on board next. "This is the time to be tough with Russia," she said on CNN.<p>
In fact, the White House is just as hard-line as ever--as Bush showed in Europe with his persistent threats against Iran. "This notion that the U.S. is getting ready to attack Iran is simply ridiculous," Bush declared at one press conference. "And having said that, all options are on the table." <p>
The White House later signaled that Bush will support European negotiations with Iran over the issue of its nuclear facilities--setting up a good cop-bad cop routine to isolate the Iranian government, while trying to shore up a new U.S.-controlled regime in occupied Iraq.<p>
A massive car bomb on February 28 highlighted the continued strength of the Iraqi resistance. The insurgency has led the U.S.--for now, at least--to try to consolidate its dominance through diplomacy. Hence Bush's make-nice rhetoric in Europe.<p>
But Bush's imperial partners are also, inescapably, imperial rivals. Simmering below the surface of Bush's stage-managed meetings with European leaders was the European Union's (EU) move to lift an embargo on arms sales to China that has been in place since that country military crushed the Tiananmen Square democracy protests in 1989. European arms dealers sold China more than $480 million worth of weapons in 2003, despite the embargo. A CIA report, cited in the <i>Financial Times,</i> sounded the alarm: "An EU-China alliance, though still unlikely, is no longer unthinkable."<p>
And so after the conquest of Iraq comes another attempt to divide Europe. When German Chancellor Gerhard Schr&ouml;der proposed that the EU have a collective presence within the NATO alliance, the Bush team bristled. For Washington, NATO is a means to maintain U.S. dominance within Europe. <p>
The White House may have shelved talk of "old Europe" versus "new Europe," but actions speak louder than words. That's why Bush traveled to Slovakia--both a NATO and EU member, where he was guaranteed a welcome reception in a small country where the U.S. is regarded as a protector.<p>
More U.S. rivalry with Europe lies ahead as Washington tries to tighten its grip on the Middle East--and with it, the world's oil supply. And if the Democrats' aren't putting up any serious opposition, it's because they agree with the overall goal.<p>
"The American claim to a dominating or hegemonic position in international affairs is bipartisan," noted columnist William Pfaff. "The Clinton administration made it; the Bush administration makes it; John Kerry made it during last year's presidential campaign. It says that America's power itself imposes a right or responsibility to suppress terrorism, nuclear proliferation, and 'rogue states,' and to enforce international order."<p>
It isn't just conservative Democrats like Kerry who hold this view. The editors of the liberal <i>American Prospect </i>magazine enthused over Bush's second inaugural address, in which, they claimed, "the president set out an attractive vision of the United States as a liberator of oppressed nations." <p>
The editors--Paul Starr, Michael Tomasky and Robert Kuttner--also embraced the Bush Doctrine, writing that "when facing a substantial, immediate and provable threat, the United States has both the right and the obligation to strike pre-emptively and, if need be, unilaterally against terrorists or states that support them."<p>
This is the pro-imperialist, copy-Bush perspective of the conservative Democratic Leadership Council--an approach that lost Kerry the election. Now those same views are being embraced by liberals looking to become the insiders in a future Democratic administration.<p>
This is further evidence that, for all the Democratic complaints about Bush's policies, when it comes to foreign affairs, there's just one party--the party of U.S. imperialism.<p>


