<font face="Arial, Helvetica, sans-serif" size="2"><b>GET THE MILITARY OUT OF OUR SCHOOLS</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Battle in an LA school</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 4, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>SARAH KNOPP talks to Los Angeles high school students who stood up to the military recruiters.</b><p>

THE ANGER against military recruiters preying on students exploded last month at Fremont High School in South Central Los Angeles. Some 5,000 students are packed onto one campus at this predominantly Latino school. Recruiters approach students during lunch hour almost daily, and even summon them during class time. <p>
Recently, several dozen students were signed up to take the Armed Services Vocational Aptitude Battery (ASVAB) without being told that it was a military test. When they were required to sign the test to give the military access to information about them, many refused. <p>
Principal Bill Higbee came to the auditorium where the test was being given, and began to yell at the students who wouldn't sign. Claritza, one of the students who refused to sign the test, said that Higbee told her, "You should be lucky to take this test. Do you want to end up working at McDonalds, or just getting married and having kids like other Latinas?" <p>
Nine students were suspended for "disruptive behavior." They immediately began to organize with other students, parents and teachers, as well as the Community Coalition of South Central LA and the Coalition Against Militarism of our Schools (CAMS). <p>
After a meeting with the principal, the suspensions were expunged from the students' records, and the administration has agreed to put restrictions on the recruiters--like limiting their time on campus to Wednesdays and giving the ASVAB only on weekends. <p>
Plus, according to Jesse, a Fremont student and a main organizer of the protesting students, "They've agreed to increase the presence of colleges on campus. Usually, the recruiters are roaming the campus, which is unfair, because the college reps don't do that. Most students and parents in our neighborhood don't want the recruiters there. But they don't stand up more because they don't think about the tactics that the recruiters use, or why people join." <p>
As Arlene Inouye of CAMS says, "All it takes is one weak moment for a student to sign up for the military. It blows me away how much easier it is to enlist than to get the resources to go to college. There are reports that kids who are worried about not passing the high school exit exam and kids who are nervously waiting to hear back from colleges are the ones who are most vulnerable. <p>
"When principals say that the military is giving 'these' kids opportunities, we have to say, opportunities for what? To kill? They make boot camp sound like a post-high school self-improvement program. I went to visit one. There is a fantasy-illusion that this is good for kids. What about how it affects their minds? It's really about death and emotional and spiritual torment. When they talk about the military as an opportunity, we have to keep focused on what the purpose of the military is. It's not about jobs, it's about war." <p>
Administrators at Fremont are hoping that the demands they agreed to will quiet the students down--and stop them from going further.<p>
But organizers have been thinking about next steps. Inouye says that "to really stop the recruiters, it would take a change to federal law. It would take legal challenges and grassroots movement. I'm not very hopeful, because military recruitment is entrenched in our system. But the culture is changing, and we're helping to do that. Now it's getting almost to the point where it's more popular to be against the war. It's more acceptable." CAMS is considering an Adopt-A-School program where community members could help anti-recruitment efforts at certain schools. <p>
"It will take a big fight because you will have to go against the federal government," says Jesse. "On a local level, it's hard, but things have been changed before. They can again, if enough people are willing to be involved." <p>

