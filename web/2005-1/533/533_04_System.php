<font face="Times New Roman, Times, serif" size="4"><b>Keyes to homophobia</b></font><p>

<font face="Times New Roman, Times, serif" size="3">HATE REALLY is a family value for right-winger Alan Keyes. Keyes was a source of endless amusement for the media during his failed senate campaign in Illinois last year, never seeming to miss the opportunity to open his mouth and promptly insert his foot. <p>
He drew fire even from members of his own party for remarking that Mary Cheney--the openly gay daughter of Vice President Dick Cheney--was a "selfish hedonist." "If my daughter were a lesbian," Keyes said at the time, "I'd look at her and say, 'That is a relationship that is based on selfish hedonism.' I would also tell my daughter that it's a sin and she needs to pray to the Lord God to help her deal with that sin."<p>
It turns out that Keyes knows what he was talking about. His daughter, 19-year-old Maya Keyes, recently came out to the media.<p>
According to Maya, her parents have known she was gay since the end of high school. Although Maya says she disagrees with her father on "almost everything" political, she worked hard for his senate campaign.<p>
The thanks she got from her parents? They threw her out of their house, refused to pay her college tuition and stopped speaking to her. "As long as I was quiet about being gay or my politics, we got along," she said. "Then I went to the counter-inaugural," the January protests in Washington against President Bush. "My father didn't like that."<p>
Maya came home from the demonstration to find out that she had been fired from her job at her father's political organization. She was told to leave her father's apartment and not to expect any money toward attending Brown University, where she had been admitted. "In my father's view, financing my college would be financing my politics, in a sense," Maya said, "because I plan to be an activist after college."<p>
The Point Foundation, a San Francisco-based charity that provides scholarships to students "who have been marginalized because of their sexual orientation," decided to pay Maya's expenses so she can begin her studies at Brown. And Maya recently spoke at a rally in Annapolis sponsored by Equality Maryland, the state's gay rights lobby, telling the crowd about what it was like growing up as a "liberal queer" in a conservative household. <br>
-- <b><i>Washington Post</i>, </b>February 13, 2005<p>

<font face="Times New Roman, Times, serif" size="4"><b>Conservatives on the prowl</b></font><p>

<font face="Times New Roman, Times, serif" size="3">FOX NEWS pundit Sean Hannity wants to help all the conservatives who might be going through a dating dry spell. He's come up with "Hannidate 2005"--a section of his Web site where conservative singles can post personal ads to look for love with like-minded folks.<p>
Gary from Ohio says that he has "good morals and values" and "is of a conservative nature." "I have all my teeth and brush them," he adds. But be warned: he says he's looking for a woman who has "good personal hygiene and has all their teeth and no rotten black teeth. I see this way too much and I find it a turn off."<p>
David, a self-described "conservative Christian" from Lansing, Mich., says that his "musical tastes are rather eclectic, basically anything except hip hop. I don't smoke and do not drink to excess."<p>
He might be a good match for Krista &#8211;also from Michigan. She describes herself as someone who loves the U.S. and the president and, revealing her tender side, says that one of her most favorite books is <i>I Love You, Ronnie</i>--Ronald Reagan's love letters to Nancy Reagan. "I want the kind of love that Nancy and Ronald Reagan had," she writes. "I am a hopeless romantic looking for a kind-hearted fun-loving, Christian guy who is willing to stand up for what he believes in and doesn't mind a strong, well educated, conservative, Christian women [sic] by his side."<br>
-- <b>ThinkProgress.org, </b>February 24, 2005<p>

