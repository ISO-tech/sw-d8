<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">March 4, 2005 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>University of Vermont</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Rose O'Connell and Erik Wallenberg</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BURLINGTON, Vt.--More than 100 faculty, staff, students and construction workers rallied outside the University of Vermont (UVM) Board of Trustees meeting February 11 calling for good jobs at UVM. <p>
University administrators are on a campaign to cut health care for all employees. The faculty union, which has experienced layoffs, is now negotiating their second contract in an attempt to protect their health care and job security. The nearly 2,000 staff at UVM have launched a union drive in response to increased health care costs and extremely low wages. <p>
A member of the union drive called for an end to gross inequities in salaries between top administrators and staff, expressing outrage that the 19 vice presidents make more than $100,000 each, and their raises alone are more than what many staff make in a year. <p>
UVM is currently spending more than $300 million on new construction projects while refusing to provide health care for construction workers on these jobs. Members of the construction crew spoke at the rally about the absurdity of not having health care while building on a campus with a medical college connected to the state's largest hospital.<p>
Inside the board of trustees meeting, a member of the faculty union called on James Pizzagalli, the CEO of Vermont's largest construction company and the chairman of the UVM Board of Trustees, to resign over this blatant conflict of interest. Pizzagalli has recently been awarded $83 million in construction contracts at UVM. Within a week of this rally, Pizzagalli's resignation was front-page news. <p>
More pressure like this and acts of solidarity between all UVM workers can force the administration to pay living wages and provide health care for all of its employees.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Defend Richard Berg</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--Management at the University of Chicago Hospitals (UCH) suspended Teamster union member Richard Berg, with a threat to fire him, in December.<p>
A longtime Teamster reform activist, Berg has worked 16 years at the University of Chicago Hospitals. In October, he ran for president of Local 743 as part of the rank-and-file New Leadership Slate to challenge the current old-guard officials.<p>
Berg initially won the vote, but the old guard leadership wasn't going to let go of the 12,000-member local that easily and forced through a second election. In the second vote in December, incumbent President Bob Walston won.<p>
As an online petition in support of Berg notes, "To any casual observer, it's obvious that the Local 743 old guard officers and UCH are collaborating to punish Brother Berg for his efforts on behalf of the workers."<p>
Supporters, who include students, community activists, members of Service Employees International Union Local 73, the Illinois Nurses Association and the Teamsters Local 743, have held pickets at the hospital in defense of Berg. They are demanding that Berg be reinstated with full back pay and no loss of benefits.<p>
