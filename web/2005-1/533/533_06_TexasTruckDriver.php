<font face="Times New Roman, Times, serif" size="4">Truck driver who was betrayed by Halliburton</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Rich man's war for oil</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 4, 2005 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">ALLAN PETTY of Burnet, Texas, experienced the war in Iraq much like the average soldier. He was under constant attack as he went about his duties; he received lousy pay from an uncaring employer; and he brought the horrors of war home with him.<p>
Unlike a soldier, though, Petty could call it quits when he'd had enough. He did just that after working only four months as a civilian contractor based in Kuwait for Kellogg, Brown and Root (KBR), the Houston-based subsidiary of Halliburton.<p>
Petty drove an unarmored flatbed truck full of military supplies between bases in Iraq. His truck was attacked on five out of every six trips, he told the <i>Washington Post</i>--with roadside bombs, mortar fire and the usual assortment of improvised weaponry. Even worse was the leisure-time activity when he returned to his base in Kuwait. He and his fellow truckers watched short videos of a beheading over and over again, then tied each other up so they could practice how to escape. Kidnappings were their greatest fear. <p>
Like the majority of soldiers, Petty took the job out of economic desperation. He was just barely scraping by at home. His goal was to make enough for a house for his wife and six daughters. When he signed up, he expected to make between $8,000 and $12,000 a month.<p>
KBR spokeswoman Stephanie Price explained salary dynamics to the <i>Post </i>with typical capitalist gobbledygook and high-sounding phrases like "the hard work and dedication of those who put their lives on the line daily." Yet Petty's bank statements showed he earned $2,000 to $4,000 a month, no more than he was making back in Texas.<p>
Petty's wife Sylvia said that he came home a different man, exhibiting most of the signs of post-traumatic stress disorder. KBR offered him counseling, but he couldn't afford the gas for the trips to Houston. "His character changed," his wife said. "He is at the bottom of the barrel."<p>
And now he can't get his old job back. "Nobody wants your daddy," he told his girls.<p>
Petty's story gives the lie to the image of the civilian contractor willing to take risks in the war for an abnormally fat paycheck. As in civilian life, the big bucks are reserved for a select few, and the average person doing contract work gets screwed.<p>
For the many who sign a contract to go to this rich man's war for oil and empire, it's more like a last-ditch chance for a very fragile brass ring that just might take you over the line to economic solvency--at least for a while, if you're lucky. A handwritten sign Petty remembers seeing in the lounge where civilian contractors gathered in Kuwait tells their story: "No one know us; we're just hurting, bleeding and dying. We're just contractors."<br>
<b>Cindy Beringer, </b>Austin, Texas<p>


