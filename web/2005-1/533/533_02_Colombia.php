<font face="Times New Roman, Times, serif" size="5"><b>Murdered by the Colombian military </b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 4, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">COLOMBIA'S MILITARY has carried out another massacre against civilians--and among the victims is a well-known human rights activist who recently spoke at a demonstration in the U.S. On February 21, the 11th Brigade of the Colombian Army killed seven people from the Peace Community of San Jos&eacute; de Apartado.<p>
The murders mark a bloody new chapter in the Colombian government's dirty war, not only against left-wing guerrillas, but all political opponents.<p>
One of the dead is Luis Eduardo Guerra, who spoke in 2002 at the annual demonstration at Fort Benning, Ga., against the School of the Americas--the U.S. military's training camp for Latin American soldiers and paramilitaries.<p>
Two days before the massacre, Luis Eduardo had traveled to his farm to harvest cocoa beans. On the morning he was due to return to San Jos&eacute; de Apartado, members of the 11th Brigade detained him, his partner and his 11-year-old son. The three were taken to the house of Alfonso Bol&iacute;var Tuberquia-Graciano, a leader of the nearby Mulatos Peace Community.<p>
On February 22, the cut-up bodies of Luis Eduardo, his partner and son, and those of Alfonso Bol&iacute;var, his partner and their 2-year-old son and 6-year-old daughter were found in a grave near the house.<p>
"I think the message [the Colombian government is] sending is that they don't want peace in Colombia," Cecilia Zarate-Laun, a member of the Colombia Support Network, and a friend of Luis Eduardo Guerra, told <i>Socialist Worker.</i> "They want war. The message they are trying to give is that the civilian population has to take sides, and any civilian organization that tries to be independent from the conflict is naturally and automatically branded as sympathizers to the guerrillas."<p>
San Jos&eacute; de Apartado was officially established as a "peace community" in 1997 after a series of right-wing massacres. It is the oldest of a number of communities in Colombia to declare themselves "neutral" in the civil war between Colombia's right-wing military and paramilitary groups, and leftist rebels. <p>
Since it was established, Colombian authorities--eager to stamp out any trace of sympathy for the rebels--have been frustrated by the community's ban on police and soldiers.<p>
As coordinator of the human rights committee of San Jos&eacute; de Apartado, Luis Eduardo became a particular target--for his willingness to speak out against military atrocities carried out against civilians. In 2002, he traveled to the U.S. to speak at the demonstration against the School of the Americas.<p>
The latest killings show that residents of San Jos&eacute; de Apartado were right to reject the government's police and soldiers--who have longstanding ties to even more violent right-wing paramilitary groups. "In a demonstration of its incredible illegitimacy, the Colombian state has carried out another massacre that bathes our land in blood," reads a statement by the community of San Jos&eacute; de Apartado, which is planning to send a delegation of 300 people to collect the bodies of the victims.<p>
That anger should be directed at the U.S. government as well--for supplying the Colombian regime for years with tens of millions of dollars in aid and military hardware in the name of "fighting the war on drugs." That money has been funneled directly into the government's dirty war.<p>
"The [Bush] administration has a big responsibility, because when it comes to increasing military aid to Colombia...they say that they don't violate human rights," says Zarate-Laun. "They shouldn't lie to the American people. They should tell them the truth. The truth is that they are not really after drug dealers. They are protecting the interests of the United States and the interests of the international corporations."<p>
