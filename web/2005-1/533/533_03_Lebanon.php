<font face="Times New Roman, Times, serif" size="5"><b>The real U.S. objective in Lebanon</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 4, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">A SUICIDE bombing in Israel blamed on Syria and the resignation of the pro-Syrian Lebanese government's cabinet quickly became the pretext for the U.S. and Israel to advance their agenda the Middle East.<p>
The U.S. State Department hailed the cabinet resignation--which took place after a mass protest in Beirut--as another example of the Washington-backed "revolutions" that forced out governments in Ukraine and Georgia. Paula Dobriansky, under-secretary of state for global affairs, said: "In Lebanon, we see growing momentum for a cedar revolution that is unifying the citizens of that nation to the cause of true democracy and freedom from foreign influence."<p>
What Dobriansky really means is that the foreign influence of Syria--anchored by its 15,000 troops stationed in Lebanon--will be replaced by the foreign influence of the U.S., with Lebanon's old colonial power France in a sidekick role and neighboring Israel as the enforcer. The Lebanese who participated in mass protests in the hopes of determining their own future are soon to be bitterly disappointed.<p>
The imperialist character of the U.S.-Israeli agenda could be seen following the February 25 suicide bombing in Tel Aviv that killed five Israelis. Israel initially blamed the bombing on Hezbollah, the Shiite party aligned with Syria and Iran, even though Hezbollah doesn't have a history of such actions. When members of the Islamic Jihad group claimed responsibility for the attack, Israeli Prime Minister Ariel Sharon--apparently with the U.S.'s blessing--kept pointing the finger at Syria, claiming that those who organized the bombing were funded by Damascus. <p>
Israel's saber rattling reinforces Washington's campaign against Syria, which began last month when the U.S. held the Syrian government responsible for the assassination of former Lebanese Prime Minister Rafik Hariri. But it's extremely unlikely that Syria would seek a head-on confrontation with the U.S. by ordering the assassination, especially with U.S. troops massed next door in Iraq. In fact, many in Lebanon believe that Israeli forces carried out the assassination precisely to provoke such a crisis. <p>
Syria, moreover, announced last month that it would pull troops out of Lebanon--and even handed over Saddam Hussein's half-brother to U.S. authorities in Iraq. But Washington won't take "yes" for an answer. <p>
The U.S.-Israeli squeeze on Syria is part of a bigger attempt to isolate Iran, which supports both Syria and Hezbollah. While Washington appears ready to back the European powers' negotiations with Iran, the threat of military strikes looms in the background--to be carried out by either the U.S. or Israel.<p>
For Bush and Sharon, the cease-fire with the Palestinians and concessions by Syria represent an opportunity to carry out further provocations elsewhere--and consolidate their control over the entire Middle East. <p>


