<font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Bush uses rhetoric about "liberation" to sell war</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Their hypocrisy about Islam</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 4, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">EVER WONDERED why the Bush administration seems so committed to promoting the "liberation" of women in the Muslim world while systematically opposing it inside U.S. borders?<p>
U.S. officials insisted that one-third of seats to the new Iraqi National Assembly be held by women. Yet it is well known that Bush vehemently opposes "affirmative action quotas" in the U.S. "The Americans have actively pushed for equality for women" in Iraq, the <i>New York Times</i> recently reported, under a headline warning "Leading Shiite Clerics Pushing Islamic Constitution in Iraq."<p>
Championing women's rights against "Islamic fundamentalism" has proven a useful tool for the Bush administration to sell its war on the Muslim world.<p>
Bush claimed to be "liberating" Afghan women from Taliban rule in November 2001--complete with smiling Afghan women lifting their veils for Western television cameras. Three years later, the vast majority of Afghan women remain forced to wear the burqa--head-to-toe Islamic covering.<p>
The Taliban's Department of Vice and Virtue has been resurrected, under the name of the Ministry of Religious Affairs, to enforce Sharia (Islamic) law. Warlords responsible for a reign of terror between 1992 and 1996, including the mass rape and murder of women, remain in power throughout the countryside, enriching themselves through opium production. And the U.S. occupation of Afghanistan continues, enforcing the new regime.<p>
This outcome was easy to predict, challenging the ridiculous notion that a right-wing Republican like Bush would take a genuine interest in advancing women's rights anywhere in the world. <p>
Such hypocrisy is not unique to the Bush administration, however. The reference points of European colonizers of Muslim nations a century ago bear a striking resemblance to those of U.S. imperialists today. <p>
British Consul General Lord Cromer, for example, claimed the British occupation of Egypt targeted "first and foremost" Islam's "degradation of women." Yet back in England, Cromer was a founding member of the Men's League for Opposing Women's Suffrage. And Britain's colonial policies aimed to develop Egypt's economy no further than as a supplier of raw materials for the British Empire.<p>
Sound familiar? The systematic denigration of Islamic culture long outlived colonial rule and remains alive and well today. <p>
Throughout the twentieth century, Western-allied Arab rulers imposed "Europeanization" on their own populations--banning Islamic dress. In 1925, Kamal Ataturk, ruler of post-Ottoman Turkey, banned the traditional fez cap for men, under penalty of death. Iran's Shah Reza Khan mandated European attire for men in 1928 and banned the hijab (Islamic veil) for Iranian women in 1936.<p>
In this context, Muslim organizations have historically embraced Islamic religious customs as a counterweight to Western imperialism. In addition, the U.S. played a role in building up the very "Islamic extremists" that its war on terror targets today. <p>
The U.S. provided $3 million for the buildup of an Islamic fighting force, known as the mujahadeen, to oust the Soviet Union from Afghanistan in the 1980s. Journalist Ken Silverstein described, "The mujahadeen fighters espoused a radical brand of Islam--some commanders were known to have thrown acid in the faces of women who refused to wear the veil." BBC correspondent Matt Frei noted, "The CIA even helped 'Arab Afghans' like Osama bin Laden, now 'America's most wanted,' to fight [in Afghanistan]."<p>
Understanding this history sheds light on the resurgence of Islam--as a reflexive hostility among most Arabs and Muslims to U.S. efforts to impose "Western values" at gunpoint today.<p>
The U.S. claim to be "liberating" Afghan women launched the "war without end" and provided the steppingstone to the invasion and occupation of Iraq. Now the U.S. claims to be protecting Iraqi women from encroaching (Iranian-backed) Islamic rule to justify its continued occupation of Iraq, while laying the groundwork for a possible assault on Iran.<p>
The <i>New York Times </i>reported on February 14, "The prospect of a...split between religious and secular parties, also appeared to signal a continuing role for the U.S. government, which already maintains 150,000 troops here, to help broker disputes." If the first claim resulted in tragedy, the second should be regarded as farce.<p>


