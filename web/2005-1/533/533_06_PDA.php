<font face="Times New Roman, Times, serif" size="5"><b>The PDA reins in the left</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 4, 2005 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">I WAS glad to read Lance Selfa's column ("Offering a lifeline to the Democrats," February 11) on the Progressive Democrats of America (PDA), particularly since much of Selfa's general critique applies to my experience at PDA's Washington, D.C., summit earlier this year.<p>
Happening on the heels of the inauguration protests, with a large number of participants sporting the soothing "Bush-Cheated 2004" pins, the conference declared its strategy as "working inside and outside" the Democratic Party. <p>
Conference attendees were treated to Rep. Jesse Jackson Jr.'s fiery sermon about the need to cloak Democratic principles in more convincing rhetoric, such as declaring universal health care a constitutional right. This would be inspiring if one suddenly forgot that the Democratic Party has never fought for universal health care and doesn't seem to favor it. <p>
These contradictions persisted for the next two days. The first public test of the inside-outside strategy comes as Howard Dean (a PDA favorite) ascends the throne of the Democratic National Committee chairmanship. But he's promised to retain much of money-man Terry McAuliffe's fundraising staff and is muzzling himself on his previous so-called "antiwar" position in deference to the legislators of his pro-war party. The doctor may be in the house, but backbone surgery isn't part of the treatment plan. <p>
Who except the most na&iuml;ve or cynical among PDA's membership, I wonder, are surprised by this continued co-optation and treachery from a party that has repeatedly rejected the advances of progressive forces not just in the 2004 election but throughout its history? Unfortunately, the PDA is a collaborator in this abusive cycle, and the organization's loyalty to the Democrats will always trump its progressivism, as even Tim Carpenter, PDA's political director, explicitly confirmed. <p>
I asked Carpenter if, after their preferred candidate is torpedoed in the 2008 primaries, the PDA would support the Party's center-right favorite. "Of course," Carpenter said. "We're Democrats."	So there you have it: the entire point is not to deliver a backbone to the Democratic Party but to prevent the independent left from developing one. <p>
This is the reason for PDA's courting of prominent leftists like Medea Benjamin--who, having performed so admirably in shepherding progressives into the Democrats' smothering "big tent" in 2004, can now be relied on to maintain the link between our grassroots movements and the next Democratic electoral train soon to come around the bend. Having not yet recovered from the nauseating ride of the Kerry campaign, we would do well to reject PDA's invitation to get on board.<br>
<b>Derek Tyner, </b>Washington, D.C.<p>

