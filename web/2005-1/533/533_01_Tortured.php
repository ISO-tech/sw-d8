<font face="Times New Roman, Times, serif" size="5"><b>Tortured to death in Abu Ghraib prison</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 4, 2005 | Page 1</font><p>

<font face="Times New Roman, Times, serif" size="3">MANADEL AL-JAMADI died an excruciating death at the hands of U.S. torturers at Abu Ghraib prison in Iraq.<p>
Al-Jamadi's death was made public last year, when the Abu Ghraib torture scandal broke--and photos surfaced of smiling U.S. soldiers posing with his corpse. <p>
But last month, the horror of his death became clear when media reports revealed that al-Jamadi died under CIA interrogation in a prison shower room--where he was suspended by his wrists, with his hands cuffed behind his back. This torture practice is called a "Palestinian hanging"--the name comes from Israeli forces, who use it against Palestinians. <p>
The revelations about al-Jamadi's death put renewed attention on the use of torture by the U.S. forces --or by other countries that Washington gets to do the dirty work.<p>
According to statements from Abu Ghraib prison guards, al-Jamadi was brought to Abu Ghraib naked below the waist, with a plastic bag covering his head. Guards then dressed him in an orange jumpsuit, slapped on metal handcuffs and took him to the shower room, where he was shackled backwards to a barred window in the torture position.<p>
After about a half-hour of "questioning" by a CIA interrogator, guards were called in to reposition al-Jamadi. Army guard Sgt. Jeffery Frost told investigators that al-Jamadi's arms were stretched so far that he was surprised they "didn't pop out of their sockets."<p>
When Frost and other guards released the shackles and lowered al-Jamadi, blood gushed from his mouth "as if a faucet had been turned on."<p>
When the military and the CIA want to use even more violent interrogation "techniques" that are against U.S. law, they "outsource" the job. Under the process known as "extraordinary rendition," the U.S. works with countries like Saudi Arabia or Syria--notorious for using abuse and torture--to capture or send detainees abroad for interrogation or prosecution. <p>
Rendition is one of the U.S. government's dirty secrets--carried out increasingly under the Clinton administration, and more commonly still under Bush. Estimates of the number of U.S.-held prisoners transferred to nations that use torture stand at 150.<p>
One victim is Ahmed Omar Abu Ali, a U.S. citizen accused of plotting with al-Qaeda to assassinate George Bush. Abu Ali was arrested by Saudi officials--reportedly with the knowledge of U.S. officials--in a crackdown after bombings in Riyadh in 2003. But he wasn't charged with a crime during the 20 months he was imprisoned in Saudi Arabia.<p>
Instead, he was whipped--sometimes for three straight days--kept in solitary confinement for months, blindfolded and denied food, his family members recently told the <i>New York Times.</i> Now, the Justice Department has brought Abu Ali to the U.S. and charged him with giving "material aid" to terrorists--for supposedly using money from "terrorist" associates to buy a laptop computer and books. <p>
There won't be any apologies from the Bush administration for its use of torture--whether by U.S. forces or their surrogates in other countries. John Radsan, a former CIA lawyer, recently explained to the <i>New Yorker: </i>"As a society, we haven't figured out what the rough rules are yet. There are hardly any rules for illegal enemy combatants. It's the law of the jungle. And right now, we happen to be the strongest animal."<p>

