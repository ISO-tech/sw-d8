<font face="Times New Roman, Times, serif" size="5"><b>FDA doing drug giants' bidding</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 4, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">MAKERS OF the highly profitable pain pills Celebrex, Bextra and Vioxx breathed a sigh of relief earlier this month when a government panel ruled that their drugs could stay on the market. All three have been under scrutiny for possibly causing heart damage.<p>
But according to a new report, the verdict was decided well before the panel met. Out of 32 advisers on the Food and Drug Administration's (FDA) panel, 10 had served as consultants for the manufacturers of the drugs in question, according to an analysis by the Center for Science in the Public Interest (CSPI) requested by the <i>New York Times.</i> The CSPI tracks scientists' ties to the drug industry, which it gathers from medical journals and other public records.<p>
Ten members of the panel had worked in some capacity in recent years for Merck, the maker of Vioxx; Pfizer, which makes Celebrex and Bextra; or Novartis, which is applying to sell Prexige.<p>
According to the <i>Times, </i>if the 10 suspect advisers had withdrawn, the panel would have voted 12 to 8 to have Bextra taken off the market. Vioxx would have been voted off the shelves 14 to 8. Panel member Dr. Curt Furberg told the <i>Times </i>that he was "uncomfortable with the Pfizer-friendly undertone" at the meeting.<p>
In addition to the 10 panelists with immediate conflicts of interest, 17 of the other scientists on the panel had ties to drug manufacturers other than the three in question. And these numbers might even be low--since panel members' ties to the drug industry may not yet be disclosed publicly.<p>
This situation is hardly unique. The FDA claims that it has a hard time finding panelists who don't have these industry ties to serve on its committees.<p>
"My employees usually vote for me as well," W. Mark Lanier, a Houston lawyer representing people who have sued Merck after taking Vioxx and suffering heart attacks or strokes, told the <i>Times.</i> <p>

