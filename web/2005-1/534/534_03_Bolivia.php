<font face="Times New Roman, Times, serif" size="5"><b>Bolivian rebellion topples a president</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 11, 2005 | Page 3</font><p>
<font face="Times New Roman, Times, serif" size="3">FACED WITH a growing social rebellion, Bolivian President Carlos Mesa submitted his resignation this week. In a speech to the country, he said he was tired of being caught in the middle in Bolivia's sharply polarized society. "I have reached the limits of the possible," he said. "I'm not going to govern according to the craziness that each sector is pushing on me."<p>
Mesa's resignation had not been accepted by the Bolivian Congress when <i>Socialist Worker</i> went to press Monday night. If Congress refuses to accept the resignation, Mesa says he will demand a "national pact" and full cooperation from opposition parties for his administration's policies.<p>
Evo Morales, the leader of Bolivia's main opposition party, described Mesa's resignation as "blackmail." Morales' Movement Toward Socialism (MAS) party wants Mesa to stay in office until the 2007 elections "in order to avoid any 'unconstitutional' succession to power." Morales fears the response of U.S. imperialism to a MAS government if it were to come to power as a result of the popular overthrow of the presently elected government.<p>
Mesa has been bitterly critical of Bolivia's social movements that have recently mounted aggressive campaigns to regain popular control of Bolivia's privatized water and natural gas. In early January, a fight led by the Federation of Neighborhood Associations of El Alto, the working-class satellite city above La Paz, forced the government to agree to cancel the city's water service contract with Aguas del Illimani, a subsidiary of Suez, the French-based transnational corporation.<p>
But Mesa also criticized the business elite of the gas-rich eastern states. Local gas magnates in the state of Santa Cruz oppose the demand of the social movements that 50 percent of the profits of transnational petroleum companies be paid to the Bolivian government.<p>
"The fight to the death for the recovery of our natural resources from the transnational corporations has begun in Boliva," declared Oscar Olivera, the principal spokesperson for the Coalition for the Defense and Recovery of Natural Gas.<p>
The current wave of unrest began on March 2 with a call for mobilizations and a "civic strike" based on an alliance between urban and rural social movements. The immediate focus of the mobilizations was the government's apparent reneging on its promise to dissolve the water contract with Aguas de Illimani. But demands over natural gas and for the convening of a Constituent Assembly were also central to the protests.<p>
Marches and roadblocks involving thousands of workers, students and indigenous organizations filled the streets of El Alto and La Paz. Solidarity demonstrations were held throughout the country and as far away as Europe. Despite Mesa's resignation, movement leaders vowed to keep up the mobilizations, which had already lasted six days as <i>SW </i>went to press.<p>


