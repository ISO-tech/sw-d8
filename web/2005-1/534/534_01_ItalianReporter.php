<font face="Times New Roman, Times, serif" size="5"><b>Italian reporter shot by the U.S.</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 11, 2005 | Page 1</font><p>

<font face="Times New Roman, Times, serif" size="3">AN ITALIAN journalist held hostage in Iraq for more than a month was released last week--only to be shot by U.S. forces.<p>
Giuliana Sgrena, a journalist for Italy's left-wing <i>Il Manifesto</i> newspaper, was taken hostage in early February while in Baghdad interviewing refugees from the U.S. assault on Falluja. A gut-wrenching video was released showing Sgrena pleading for her life--and for the withdrawal of foreign troops from Iraq.<p>
Last week, a team of Italian negotiators, including intelligence officer Nicola Calipari, secured Sgrena's release after the Italian government reportedly paid a large ransom. But as the car carrying Sgrena and the negotiators made its way to the Baghdad airport, it came under heavy fire--from U.S. forces. <p>
According to Sgrena, "300 to 400" bullets were fired "without any justification" by a U.S. patrol. Sgrena was wounded in the left shoulder and lung. Calipari died instantly from a shot to the head. Two other passengers in the car were also wounded.<p>
The U.S. claims its soldiers fired only after the car failed to slow down for a checkpoint--supposedly despite repeated warnings. But Sgrena told Italian television, "There was no bright light, no signal and no checkpoint--it was a patrol." <p>
As Sgrena wrote in <i>Il Manifesto</i>, "The driver said that we were heading toward the airport that I knew was heavily patrolled by U.S. troops. They told me that we were less than a kilometer away when...I only remember fire. At that point, a rain of fire and bullets hit us, shutting up forever the cheerful voices of a few minutes earlier. Nicola Calipari threw himself on me to protect me, and immediately--I repeat, immediately--I heard his last breath as he was dying on me."<p>
White House adviser Dan Bartlett called the attack a "horrific accident," but Sgrena says that she believes it was deliberate--because U.S. officials are opposed to meeting the ransom demands of insurgents. "The fact that the Americans don't want negotiations to free the hostages is known," she told Sky television. "So I don't see why I should rule out that I could have been the target."<p>
While this attack captured headlines worldwide, killings by U.S. soldiers in Iraq--at checkpoints and elsewhere--take place daily. They don't make the news because the victims are Iraqis.<p>
Italy's right-wing Prime Minister Silvio Berlusconi immediately pledged that his government would continue to support Washington's occupation. Communications Minister Maurizio Gasparri told one news agency, "The military mission must carry on because it consolidates democracy and liberty in Iraq."<p>
This defense of the war prompted outrage among ordinary Italians, a large majority of whom are opposed to the U.S. occupation. At a demonstration last month calling for Sgrena's release from captivity, an estimated 500,000 turned out in Rome, carrying signs that read "Free Giuliana" and "Free the Iraqi people." Now, the U.S. attack on Sgrena is sparking a new protests against the U.S. and Berlusconi--with renewed calls for the withdrawal of Italy's 3,000 troops stationed in Iraq.<p>

