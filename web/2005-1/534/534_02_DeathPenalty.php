<font face="Times New Roman, Times, serif" size="4">Supreme Court bans execution of juvenile offenders</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Another blow against the execution system</b></font> <p>
<font face="Arial, Helvetica, sans-serif" size="2">March 11, 2005 | Page 2</font><p>
<font face="Times New Roman, Times, serif" size="3">THE U.S. Supreme Court banned the death penalty for juvenile offenders in a decision handed down last week. The ruling comes three years after the justices barred the execution of the mentally retarded--and amid a slowdown in juries handing out death sentences. MARLENE MARTIN of the Campaign to End the Death Penalty explains the significance of the decision.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">THE U.S. Supreme Court decided in a 5-to-4 vote to join the rest of the world in banning the execution of juvenile offenders. As the justices' majority opinion noted, "Only seven countries other than the U.S. have executed juvenile offenders since 1990: Iran, Pakistan, Saudi Arabia, Yemen, Nigeria, the Democratic Republic of Congo and China." <p>
Seventy-two juvenile offenders on death rows across the country will now have their sentences reduced--most, presumably, to life without the possibility of parole.<p>
In 1989, the court ruled against the execution of 15 years olds, but decided it was okay to put 16- and 17-year-olds to death. In banning the execution of juveniles now, the justices didn't acknowledge that they were wrong back in 1989. Instead, they say that "national standards" for what constitutes "cruel and unusual punishment" have shifted.<p>
The majority side in the decision cites the fact that five states plus the federal government have banned executions of juveniles since the death penalty was reinstated in 1976, and that while 19 states allow such executions, only three--Texas, Oklahoma and Virginia--have executed juvenile offenders in the last decade. <p>
Unusually, they also cited the influence of international standards. In the majority opinion, Justice Anthony Kennedy wrote, "[It ] is fair to say that the United States now stands alone in a world that has turned its face against the juvenile death penalty."<p>
This ruling is an important step forward for abolitionists--as was the Supreme Court's decision three years ago to ban the execution of the mentally retarded. It is another blow against the death penalty system.<p>
Now, the question is how we will end executions altogether. It can be proven that the death penalty is given out almost exclusively to defendants who are poor and disproportionately minorities. The system also makes horrendous mistakes--since the death penalty was reinstated, at least 118 people have been proven innocent and released from death row.<p>
Aren't these "facts" enough to warrant banning executions altogether? You would think so. But the Supreme Court needs more than persuasive arguments--it needs pressure from society to convince it that "evolving standards of decency" require it to end the death penalty. <p>
In plain English, the justices are admitting in their decisions that they are influenced by public opinion. That's why we have to continue to build a grassroots movement bringing together the exonerated death row prisoners, those still on death row, family members and activists to expose the reality of the death penalty today--that it is barbaric, racist and aimed at the most vulnerable in society. It is no coincidence that the death penalty was first overturned by the Supreme Court in 1972--on the heels of the civil rights movement , when support for the death penalty nationwide was at an all-time low.<p>
I spoke recently with Billy Moore and Alan Gell--two men who both spent time on death rows in the South. Both had the same response to the Supreme Court decision on juveniles: We welcome it, but what took them so long? Both expressed exasperation at the slow pace of the courts--and wondered if some of the 22 juvenile offenders executed since the reinstatement of the death penalty would still be alive today if the justices had moved more quickly.<p>
We've taken an important step forward, but we have to keep stepping--until we stamp out the death penalty completely.<p>

