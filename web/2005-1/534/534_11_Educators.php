<font face="Times New Roman, Times, serif" size="5"><b>Big turnout for educators' antiwar meeting</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Kiersten Greene, United Federation of Teachers</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 11, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--About 750 teachers and students turned out for the Educators to Stop the War Conference at Hunter High School March 5.<p>
The overwhelming message of the conference was that we have to bring the troops home now. The day was comprised of a series of democratically run debates and roundtable discussions, during which a variety of ideas and strategies were shared on topics from "Teaching War and Peace" and "Creating a Student Antiwar Movement" to "Antiwar Organizing in Union Locals" and "Recruiters and the Draft."<p>
The conference buzzed with a "what can we do?" energy. People gathered in between workshops, different union locals met during lunch to discuss possible resolutions, and participants repeatedly raised the connection between the war at home and war abroad. <p>
The conference was a project of U.S. Labor Against the War and originated from a call put out by the Professional Staff Congress (PSC), which represents the faculty of the City University of New York (CUNY). Members of other teachers' unions joined in. The conference drew people from all over the East Coast. It featured members of Military Families Speak Out and Iraq Veterans Against the War as well as family members of GI resisters such as Victor Paredes, who has been organizing support for his brother, Pablo Paredes, who publicly refused to board his ship to return to Iraq. <p>
In a roundtable discussion, "How to Stop the War: Theories and Strategies," Andy McInerney, professor at Bronx Community College said, "The people are ready to fight." He pointed out that New York City has a surplus of $2.5 billion, but that the United Federation of Teachers has been working without a contract for almost two years.<p>
Sylvia Federici, a professor at Hofstra University said, "We have to identify the disease before we even think of talking about a cure," as the discussion turned toward a critique of the capitalist system.<p>
The jam-packed closing plenary featured CUNY professor and PSC President Barbara Bowen; CUNY professor Anthony O'Brien; New York high school teacher Megan Behrent; high school student Lusmaia Diaz; and Tom Roderick, executive director of Educators for Social Responsibility. The speakers talked about the need to rebuild the antiwar movement in the labor movement, build teacher-student alliances and participate in upcoming protests.<p>
The large turnout and the energy and enthusiasm at the conference were a clear sign that we can rebuild a vibrant antiwar movement that demands an end to the occupation of Iraq.<p>


