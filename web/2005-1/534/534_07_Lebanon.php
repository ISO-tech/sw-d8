<font face="Times New Roman, Times, serif" size="5"><b>Using Lebanon to justify the Iraq war</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 11, 2005 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">LEAVE IT to the purveyors of conventional wisdom to find in Lebanon's so-called "cedar revolution" the silver lining of the 2002 U.S. invasion of Iraq.<p>
Daniel Schorr--who was once a good enough journalist to have made Richard Nixon's enemies list--congratulated George W. Bush in a National Public Radio commentary. "During the run-up to the invasion of Iraq, President Bush said that 'a liberated Iraq can show the power of freedom to transform that vital region,'" Schorr said. "He may have had it right."<p>
So not only does the seeming victory of a nonviolent movement for democratic change in Lebanon justify Bush's agenda of "promoting democracy" in the Middle East, now it's being marshaled to justify the invasion of Iraq after the fact.<p>
No one should mourn Syria's retreat from Lebanon. But no opponent of Bush's war in Iraq should think that a Syrian withdrawal from Lebanon--or the Egyptian government's pledge to allow elections with more than one candidate--justify the criminal invasion of Iraq.<p>
Let's remember that "promoting democracy" was <i>not </i>the ostensible rationale for the invasion in 2003. But with no evidence of "weapons of mass destruction," democracy promotion became the latest after-the-fact justification for the war.<p>
The stench of hypocrisy in Bush's calls for Syrian withdrawal from Lebanon is enough to require a gas mask. Bush and his friends in the Israeli and (yes) French governments pointedly demand an end to Syria's "occupation" of Lebanon. Meanwhile, the U.S. is the biggest occupying power in the Middle East, maintaining 10 times as many troops in Iraq as Syria has in Lebanon. And Israel illegally occupies the West Bank, Gaza and Syria's Golan Heights--occupations that have been widely condemned since 1967.<p>
What's more, for most of the last 30 years, the U.S. tolerated--and at times encouraged--the Syrian occupation of Lebanon. This history is so buried that even liberal writer Tom Engelhardt, author of the TomDispatch.com Web site, admitted that he didn't know it until last month.<p>
Syria intervened in 1976 during the Lebanese civil war, with the backing of the Arab League. With the Palestinian-allied left on the verge of routing the right in the war, Syria invaded Lebanon on the side of the right. The Syrian regime concluded that having a right-wing government allied with Israel in power in Lebanon was preferable to having a Lebanon controlled by leftist militias. Not coincidentally, the U.S. agreed. It acknowledged the "positive role that the Syrian government play[ed] in Lebanon."<p>
Later, George Bush Sr. acceded to Syria's virtual takeover of Lebanon in 1990-91 as the price to be paid for Syrian support of the first Gulf War against Iraq. As <i>Newsweek</i> put it in 1991: "[Syrian President Assad (father of the current Syrian president)] has already been paid off handsomely for his stand against Iraq: the gulf Arabs have committed billions in much-needed cash; Washington gave him international respectability and turned a blind eye to his absorption of Lebanon."<p>
The demand of the Lebanese to get Syria out of their country has been growing for years. But it really gathered momentum when the Syrian-backed government unilaterally extended its mandate last September--at a time when it wasn't clear whether Bush or John Kerry would be the next U.S. president.<p>
The February assassination of former Lebanese Prime Minister Rafik Hariri gave Bush Jr. and his Israeli allies an opportunity to win a longstanding strategic aim that has nothing to do with supporting "democracy" in Lebanon. Both the U.S. and Israel view Syria, with its alliance with Iran and the Lebanese militant group Hezbollah, as one obstacle to their total control of the Middle East. Weakening Syria by means of supporting its Lebanese opponents is certainly preferable to the U.S. than sending in the Marines.<p>
By the way, the last time that the U.S. mounted a full-scale invasion of Lebanon, in 1958, the Eisenhower administration was trying to quell a popular uprising that erupted after the U.S. tried to extend the mandate of a right-wing government that was widely expected to lose upcoming democratic elections.<p>


