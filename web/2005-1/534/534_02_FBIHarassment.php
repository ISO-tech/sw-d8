<font face="Times New Roman, Times, serif" size="5"><b>Antiwar activists harassed by FBI</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 11, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">AN ANTIWAR activist and socialist in New York City was visited by the FBI in late February in what could be a new stage in the federal government's effort to silence dissent.<p>

