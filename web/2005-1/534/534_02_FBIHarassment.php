<font face="Times New Roman, Times, serif" size="3">Charles Post, a member of the socialist group Solidarity, said FBI agents came to his home and questioned his partner about Solidarity "and its possible connection to a young man who was arrested for allegedly vandalizing a military recruitment center in the Bronx, N.Y.," he wrote in an e-mail posted online to other activists. Post said he viewed the visit as an attempt at intimidation because of his work in the antiwar movement.<p>
Since the September 11 attacks, the Feds have carried out a witch-hunt against Arabs and Muslims. While they have borne the brunt of these "visits" from the FBI, other activists, including students protesting military recruiters, have also been targets.<p>
The questioning may seem routine, but it is often part of a larger "fishing expedition"--designed to gain information about antiwar activities. This is a tactic that the government has never been shy about employing, particularly during wartime. "Whether it is the beginning of more generalized harassment, we feel it is important that socialists, radicals and antiwar and global justice activists generally understand their rights when approached by the FBI," Post wrote.<p>
According to the National Lawyers Guild, those rights include: the right to refuse to answer questions, the right to have a lawyer present, and the right to refuse to consent to a search<p>
"Unless they have a warrant or subpoena, you are under no obligation to answer any questions nor permit agents in your home," Post wrote in his e-mail. "We <i>strongly recommend</i> that you do not answer any questions from the FBI. They may--as they did when they questioned my partner--assure you that they are 'not out to get you.' However, anything you tell them can be used against you or other activists in the future. Silence--which is your right--is your best policy. If the FBI comes with a warrant or subpoena, we strongly recommend you contact a lawyer immediately."<p>
Activists should not be bullied into talking when the FBI comes calling. We have the right to speak out against Washington's unjust wars--and we can't be intimidated into silence. <p>

