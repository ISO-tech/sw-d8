<font face="Times New Roman, Times, serif" size="5"><b>A dead man in shackles</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 11, 2005 | Page 4</font><p>
<font face="Times New Roman, Times, serif" size="3">THE CRIMINAL justice system in this country is sick. I've never heard of a dead man being shackled and guarded around the clock--until now. <p>
"How does a dead man do time?" This is a question the Provencio family had after being told that 28-year-old Daniel Provencio had to finish out his sentence, despite the fact that he was declared brain dead on January 20. <p>
Daniel was shot in the head in prison January 16 by a "non-lethal" foam bullet. Apparently, he refused to lie down and tried to prevent guards from breaking up a fight between other inmates. To add insult to injury, the Department of Corrections actually took the time to consider allowing Daniel--who is now on a mechanical ventilator and feeding tube in a hospital--to be released on "early parole." <p>
Not only did the prison system use lethal force in a situation that in no way merits such actions, but they have a man who is on full life support being guarded and shackled by both ankles to the hospital bed. The price tag for being guarded around the clock is $1,056 a day! <p>
Even some members of the government admit the absurdity of the situation. State Senate Majority Leader Gloria Romero was quoted as saying, "It's moronic that we were ever in the situation where we were having to guard...a dead man."<p>
This is just another example of just how flawed and broken the prison-industrial complex is. The system does nothing to deter crime in our communities and is actually a distraction to the real problems in society: poverty and inequality. If our government spent as much money in our communities in the form of jobs and education as they spent on war and building prisons, crime as we know it would be drastically reduced.<p>
Instead of rehabilitating, educating and helping people turn their lives around, the system institutionalizes inmates, creating this huge revolving door. They are essentially set up to fail in the outside world. The priorities of the system are backwards and benefit only a few people. Things will not change unless we organize and make them change. <br>
<b>Lucas Nevarez, </b>San Francisco<p>

