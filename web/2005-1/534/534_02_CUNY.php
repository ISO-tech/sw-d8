<font face="Times New Roman, Times, serif" size="5"><b>N.Y. students challenge Pataki's tuition hikes</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Peter LoRe</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 11, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK Gov. George Pataki is once again trying to balance the state budget on the backs of low-income, working-class students in the city and state university systems. But City University of New York (CUNY) and State University of New York (SUNY) students are organizing--the latest in a long history of fightbacks--to stop Pataki.<p>
After two years of tuition hikes and budget cuts that have led to mounting tuition bills and larger, under-funded classrooms, Pataki, along with the CUNY board of trustees, is proposing a $250-a-semester tuition hike at CUNY's 19 universities. In the SUNY system, the proposed tuition increase is $500 per semester.<p>
In both cases, the hikes come on top of an "automatic annual index increase," which is set for 4 percent this year. The automatic hike is based on current levels of state funding for SUNY and CUNY. If state spending decreases further, the automatic increase will be considerably higher than 4 percent.<p>
In other words, CUNY and SUNY students are looking at total tuition increases that could run as high as 10 or 15 percent next year.<p>
Pataki was also taking aim at the state's Tuition Assistance Program (TAP). A majority of CUNY and SUNY students receive money from TAP, an important alternative to loans, which many students have problems paying back in a timely fashion.<p>
Pataki's plan was to have students pay out of pocket for half the cost covered by TAP. Students would only see this money if they graduated in four years or less--this in a college system where the students' average age is 25 and the average time to graduation is between five and six years. Reportedly, Pataki's attack on TAP was taken out of the proposed budget following a letter-writing campaign initiated by the New York Public Interest Research Group (NYPIRG).<p>
The fightback against TAP cuts isn't the only sign of resistance. On the first day of classes, NYPIRG held a demonstration outside CUNY's Hunter College in Manhattan to protest Pataki's proposed tuition hike. "Along with the 250 dollar hike, they are also cutting SEEK [an aid program for the poorest students], Direct Aid and money in the form of book vouchers," said NYPIRG's Robyn Citizen. <p>
The Undergraduate Student Government, NYPIRG and the International Socialist Organization have been holding meetings at Hunter to discuss how to oppose the attacks. Organizing is taking place at other CUNY campuses, and a system-wide coalition has formed.<p>
Meanwhile, the Professional Staff Congress (PSC), the faculty union at CUNY, which is going through a contact struggle, is organizing as well. The PSC recently held a picket to "demand a good contract and no tuition hikes," and is working with students in the CUNY coalition.<p>
CUNY students have faced attacks like this in the past and defeated them. In 1995, when Pataki tried to impose tuition hikes and budget cuts, students organized marches, walkouts and strikes to stop the state's attack.<p>
We need to look at this history of what has stopped tuition hikes in the past--and confront new questions, such as the connection of the U.S. "war on terror" with attacks on workers and the poor at home. "They're taking money out of us, and putting it towards the war," said Juan, a City College student. "That's why I fight them." <p>


