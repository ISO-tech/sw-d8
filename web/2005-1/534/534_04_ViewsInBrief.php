<p><a name="Deal"></a><font face="Times New Roman, Times, serif" size="4"><b>Ch&aacute;vez's deal with Uribe</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I WAS pleased to read Lee Sustar's summary of the last World Social Forum held in Porto Alegre, Brazil ("WSF 2005: Crossroads for the movement," February 4). As a participant in the World Social Forum, I also found that the WSF was silent and held any criticisms it may have had about the neoliberal orientation of the government of Luiz In&aacute;cio "Lula" da Silva. <p>
On the other hand, there was an overwhelming enthusiasm voiced among participants in support of Venezuelan President Hugo Ch&aacute;vez. People frequently declared that the Ch&aacute;vez government is not a progressive left government, but a revolutionary government.<p>
However, Ch&aacute;vez has not only been cozying up to Lula, as Lee Sustar notes in his article, but he also recently met with Colombian President &Aacute;lavaro Uribe. Uribe is Washington's conduit for prosecuting the "war on drugs" in Colombia. Ch&aacute;vez is quoted in the press as saying "We've [Ch&aacute;vez and Uribe] decided to turn a page...to work together in the construction of a common project that benefits our people." Meanwhile, Uribe had already met with Bush and will be receiving $741 million for Plan Colombia in 2006.<p>
Plan Colombia will not benefit Colombians nor Venezuelans. Ch&aacute;vez's meeting with Uribe exposes the real contradictory role that Ch&aacute;vez is forced to play: to foment good relations between Uribe and big business in Venezuela on the one hand, and on the other, to act as the conciliator between the former two and the radicalizing Venezuelan population.<br>
<b>Nate Moore, </b>Brazil<p>
