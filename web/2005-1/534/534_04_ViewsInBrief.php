<font face="Times New Roman, Times, serif" size="4"><b>Iraqi workers' union rights</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WHILE MOST of the news coming out of Iraq focuses on the armed resistance against U.S. and so-called coalition troops, Iraqi workers have been engaged in labor struggles. <p>
Most of these struggles have involved members of the Federation of Workers Councils and Unions (FWCU) and the independent Southern Oil Company Union (SOCU). Since these unions are not part of the Iraq Federation of Trade Unions (IFTU), they are technically illegal. <p>
Last year, the U.S. puppet government passed legislation declaring the IFTU the only legal labor federation in Iraq. Unlike the FWCU and SOCU, the IFTU supported the Allawi government, and does not call for ending the U.S. occupation of Iraq by the U.S. The FWCU and SOCU have denounced the U.S. occupation.<p>
Last August, by going on strike, the SOCU won pay raises for the lowest-paid workers at one oil company, and succeeded in replacing 1,000 foreign workers with Iraqi workers. During another strike, the U.S. military attempted to face down striking Iraqi oil workers through the use of armored vehicles.<p>
In the city of Kut, textile workers organized a protest to demand better pay and working conditions. Groups of armed men wearing masks showed up and fired on the workers, wounding four. Other labor struggles have involved electrical power workers in Nasriyah and Basra, and workers in chemical, plastic, and soft drink plants in Baghdad. <p>
Supporters of workers' rights need to support the right of Iraqi workers to organize in the unions of their choice--and to have the right to engage in strikes and other activities to improve their working and living conditions.<br>
<b>Ken Morgan,</b> Madison, Wis.<p>
