<p><a name="Resist"></a><font face="Times New Roman, Times, serif" size="4"><b>Support the right to resist</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I'VE BEEN increasingly disturbed by the number of antiwar activists who hold the idea that they can't support the Iraqi resistance. Support for resistance to the occupation is a lynchpin in our movement; without it, our ideology cannot hold. <p>
Anyone who denies the Iraqi people the right to resist allows the Bush administration to hold American troops hostage to the expansion of U.S. military domination. They would in essence be saying to the Iraqi people, "I know that our troops are there illegally. I know that they are torturing your people. I know that in the last 14 years American policy has killed nearly 2,000,000 Iraqis. But it would be wrong for you to defend yourselves because you might hurt an American." <p>
It is an illogical, untenable position. So why do any antiwar activists hold it? They lack confidence in the sanity of working class people in America. They lack confidence in their own ability to explain to people the logic of the resistance. They lack the politics to accurately analyze the situation both at home and abroad. <p>
The simple truth of the matter is that the death of American soldiers is not the fault of the Iraqi resistance, but of the American ruling class who demanded this war through its domination of the White House, Congress and the mainstream media.<p>
Can anyone believe that lives would have been saved if the Iraqi people had acted irrationally and greeted their murderers with flowers? The U.S. military would have been in Syria by the end of 2003. Had the Syrians capitulated, it would have been on to Iran and North Korea and beyond. <p>
In truth, the only people who would have benefited from a decision not to resist on the part of the Iraqi people are the American ruling class and their servants. The entire rest of the world owes the Iraqi resistance a debt of gratitude.<br>
<b>Cesar Montufar,</b> Los Angeles<p>

