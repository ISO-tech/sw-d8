<font face="Times New Roman, Times, serif" size="4">Schwarzenegger stops patient-to-nurse ratio law</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Helping hand for hospital bosses</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 11, 2005 | Page 4</font><p>
<font face="Times New Roman, Times, serif" size="3">ON NOVEMBER 4, 2004, two days after the Republican-dominated national elections, an emboldened California Gov. Arnold Schwarzenegger issued an "emergency" regulation to stop the state's 5-to-1 patient-to-nurse ratio law for three years. <p>
Interestingly, his "finding of emergency"--his official excuse for stopping the law--read as if it were copied from the hospital lobby's Web site. Was this a real emergency? Was the governor protecting Californians from a real danger, or was he merely parroting the hospital industry's propaganda and doing their bidding?<p>
The closure of the tiny, 39-bed Santa Teresita Hospital in Duarte, Calif., was hailed by the California Hospital Association (the hospital owners' lobby) as the "tragic consequence of unrealistic nurse ratio regulations," and it figured prominently in Schwarzenegger's "finding of emergency." In reality, this failure was actually caused by years of "severe financial distress," and nurses who worked there reported they were "generally meeting the ratios without difficulty." <p>
The real kicker to this story is this: The letter announcing the hospital's closing is dated January 6, 2004--five days after the 6-to-1 ratios started. Now tell me: Who, besides our governor, would really believe that five days of intolerable patient-to-nurse ratios caused the downfall of this poor little hospital? <p>
The governor's "finding of emergency" cites California's nursing shortage as the main reason for delaying the ratios. Ironically, the law was the primary factor in the dramatic increases in the number of nurses over the last few years, as California hospitals aggressively hired nurses to comply with the ratios. <p>
For the five-year period from 1990 to 1995, the number of active California nurses grew at a rate of less than 2,000 per year. From 1995 to 2000, the rate was 3,000 per year. But from June 2000 to June 2004, this rate exploded to 10,000 a year! And for the last six month period--from June 30 to December 31, 2004--an average of 1,000 active-status nurses were added to California's rolls each month.<p>
This welcome trend was instantly reversed by the chilling effect of the governor's "emergency" delay tactic. The January 2005 numbers showed a drop of over 300 active nurses as hospitals that had hired in anticipation of the scheduled January 1 5-to-1 ratios suddenly discovered they were "overstaffed."<p>
Certainly it costs something to hire more nurses, and there are as many guesses for how much as there are guesses for how many nurses we'll need to fill those ratios. <p>
But amazingly, there may be no cost at all. There have been dozens of independent studies showing that an enriched nurse presence actually saves the hospitals money. These studies show that when nurse staffing is pumped-up, wonderful things begin to happen: There are fewer wound infections; fewer cases of pneumonia; fewer pressure ulcers; fewer urinary tract infections; patients get better educated on issues of health, their disease and treatment; they get up and move around sooner; and they go home earlier. <p>
But traditional hospitals get paid by the patient or his insurance company to provide services. The more infections, ulcers, medication errors, late antibiotics, infiltrated IVs, pneumonias, falls, poor patient education and time spent in the hospital, the more money they get paid. <p>
I figure the best thing the hospital owners could do (if they only cared about their bonuses, and not about the health of us Californians) is eliminate as many nurses as possible from the hospitals. Come to think of it, that's exactly what happened in the 1990s, and that's what led the nurses to push for their ratio law. Interesting, huh?<br>
<b>Mike Kirchubel, </b>Fairfield, Calif.<p>


