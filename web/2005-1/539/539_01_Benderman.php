<font face="Times New Roman, Times, serif" size="5"><b>Pentagon crackdown on GI resisters</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder and Jorge Torres</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 15, 2005 | Page 1</font><p>

<font face="Times New Roman, Times, serif" size="3">MILITARY RESISTER Kevin Benderman was barred from traveling to speak at antiwar meetings last weekend, the latest escalation in the Army's harassment of him.<p>
After taking part in the invasion of Iraq in 2003, Benderman came to question the war. With his unit scheduled to redeploy to Iraq late last year, he applied for conscientious objector status, which the Army refused to grant.<p>
In January, Benderman was charged with desertion--and the Army is now seeking a general court-martial against him. The trial is at Fort Stewart on May 11. If convicted, Benderman faces up to seven years confinement, a dishonorable discharge and forfeiture of all pay and allowances.<p>
Benderman had agreed to speak at several events in Chicago organized by Voices in the Wilderness--on April 9 and 10, a weekend when he wasn't scheduled for work. But the Army abruptly denied him permission to travel.<p>
On March 19, the second anniversary of the U.S. invasion of Iraq, Benderman spoke at a "Bring the troops home now!" rally in Youngstown, Ohio. Apparently, the military didn't want a repeat of his public appearance, so Benderman's commanders denied his travel permit.<p>
To Monica Benderman, Kevin's wife, the lesson for those considering joining the military couldn't be clearer. "It is important for these students to consider just how much of their freedom is taken away against their will by the military command," she told <i>Socialist Worker.</i> "They need to be made aware of how the military controls soldiers, families, etc. The extent is an incredible violation of human rights, in our opinion."<p>
The military is anxious to squelch the growing dissent within its ranks. Pablo Paredes, who last December refused to board his Navy ship that was headed for the Persian Gulf, is likewise facing a special court-martial for filing a conscientious objector application.<p>
And two days after Benderman was denied permission to travel, a federal appeals court ruled against Emiliano Santiago, an Oregon reservist who had filed a lawsuit late last year challenging the military's "stop-loss" policy. <p>
The policy allows the military to extend the enlistment term of soldiers--forcing them to serve years beyond their original discharge date. The Pentagon implemented this program as a way to "retain" National Guard and Reserve troops, who currently make up more than 40 percent of soldiers in Iraq. Under "stop-loss," Santiago might not be allowed out of the National Guard until December 2031--even though he only signed an eight-year enlistment contract.<p>
Emiliano explained to <i>Socialist Worker</i> that he had been recruited in high school at the age of 18. "The main thing was the uniform," said Santiago. "But I'm just done with it now. I want to get out." <p>
Signs of discontent spreading in the military. Since the war began, more than 5,500 troops have gone "absent without leave" (AWOL). In Benderman's unit alone, 17 troops went AWOL, and two attempted suicide to avoid redeployment.<p>
Military resisters deserve the support of the antiwar movement--and by their actions, have the power to undermine the U.S. war on Iraq.<p>

