<font face="Times New Roman, Times, serif" size="5"><b>UC workers ready for one-day walkout</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Kathryn Lybarger, AFSCME Local 3299, UC-Berkeley</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 15, 2005 | Page 12</font> <p>

<font face="Times New Roman, Times, serif" size="3">SERVICE WORKERS at the University of California (UC) are ready to strike at the system's nine campus and five medical centers on April 14.<p>
The 7,300 workers represented by American Federation of State, County and Municipal Employees (AFSCME) Local 3299 have been fighting for a new contract since June 2004. So far, the union--which represents food servers, janitors and other low-paid workers--hasn't been able to move UC anywhere near a respectable contract.<p>
Frankly, our demands are modest. The union is seeking a pay increase, but UC won't guarantee anything. UC claims poverty, but last year, it somehow found $2.4 million to hand out in bonuses for just 65 people--at the top of the university system's administration, of course. Plus, UC diverted $20 million in private funds that were earmarked for employee raises--to build new buildings and recruit even richer executives. This, while UC's food service workers' incomes are so low that they qualify for nine major publicly funded welfare programs.<p>
Workers are also asking UC to consider seniority in hiring, promotions and pay, and to maintain fair and safe workloads for custodians. Union members also want their contract to ensure that their uniforms will not be made in sweatshops. But UC refuses to even allow this demand to come to the bargaining table.<p>
This will be the union's first strike at the state university system in 29 years. Union members are coming to understand that it will take militant action to squeeze even the smallest concessions out of the administration. After 29 years without a strike, it's time to rebuild our power from the bottom up, and this one-day strike is a start.<p>
At a strike authorization vote in March, the turnout was the largest in years, with 92 percent choosing to authorize a strike. The prospect of a one-day walkout is tapping into the anger that's been brewing for years against the priorities of state and university officials--and building workers' confidence to rebuild our union and reverse the attacks. Or as many of us are saying, "We can't take this anymore, so we have to do something, and we have to start somewhere."<p>
Support for AFSCME workers among students, other university workers and community members is growing. They're writing letters, rallying and planning to support our picket lines.<p>
What happens after our strike will be crucial, because there's good reason to expect that UC still won't bargain fairly after the one-day strike. We should be prepared to take further militant action, including civil disobedience, as needed.<p>
We may strike again, possibly alongside other university workers, like the clericals in the Coalition of University Employees, and professional and technical workers in Union of Professional and Technical Employees--because we understand the potential power in combining our forces. We're ready to fight.<p>

