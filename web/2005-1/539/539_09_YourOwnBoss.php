<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Being your own boss" is no solution</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 15, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">ONE OF the great dreams promoted in the U.S. is being "your own boss"--starting up a small business where there aren't any foremen or managers bossing you around for lousy pay and benefits.<p>
The dream is, ironically, a backhanded acknowledgement of the way in which working for wages under capitalism is alienating, tedious and unrewarding. We work to live, not the other way around.<p>
Workers own neither the machinery nor the product, and have very little control over what is produced or how it is produced, nor usually their wage level or working conditions. But the dream is also part of promoting the idea that individuals can make something of themselves, not through collective struggle, but by dint of individual spunk and hard work.<p>
This "Horatio Alger" ideology supports the idea that individuals who don't make it simply haven't tried hard enough. In short, it helps justify cutthroat capitalism by encouraging workers to "get in on the game" rather than by changing the rules.<p>
The first thing to understand is that the economy is dominated by employers who operate with 100 employees or more. According to government statistics from 2002, 99.7 percent of all private employers are small business owners, defined as businesses employing 500 people or less.<p>
But these statistics don't tell the whole story. Large businesses that employ more than 500 workers constitute only .3 percent of all employers, but they account for about half of the employed workforce and about half the total revenue. Businesses employing 100 workers or more account for more than 71 million out of a workforce of more than 115 million.<p>
True, there are a lot of small businesses. But the problem with the dream of owning your own business is that it is a precarious existence that often ends in bankruptcy. Fifty-three percent of small business owners work out of their home, and according to Small Business Administration statistics, those who are self-employed and don't hire anyone else to work for them can't get enough hours. A third of those who employ people need to supplement their business income in other ways.<p>
Moreover, only half of newly created small businesses are still in business after four years. Indeed, every year about as many small firms close as are created. In 2002, for example, 550,000 new firms opened but 584,500 closed--and, out of that, 38,150 ended in bankruptcy.<p>
For most people, having your "own business" means being self-employed (or working with a partner or a couple of helpers) and getting an insufficient amount of consistent work to stay solvent. For millions more, it simply means pouring your life savings into a business venture that doesn't work out.<p>
That small minority that are lucky enough to grow into real businesses end up surviving by exploiting labor just like a big employer--profiting on the difference between labor's output and labor's pay.<p>
The economy is dominated by giant corporations that employ millions of workers. General Motors, the world's biggest manufacturing corporation, alone employs 1.3 million people worldwide and produces revenues of $256 billion.<p>
Most workers cannot improve their lives by opting out of wage labor. Nor can they solve their problems by breaking apart the businesses they work for and taking a piece of the company for themselves. Unlike peasants, workers can't seize and divide up the employers' estate. They have to fight it collectively--and ultimately seize it collectively.<p>

