<font face="Times New Roman, Times, serif" size="5"><b>The Democratic Party's vanishing act</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 15, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THE REPUBLICANS should be taking a beating. Discontent with George W. Bush's Social Security privatization scam has sent the president's approval rating plunging to a new low of 44 percent, just three months after beginning his second term. <p>
Then there's the most powerful Republican in Congress, House Majority Leader Tom DeLay (R-Texas). He's alienated most people in the country by leading the effort to ram through a federal law to intervene in the Terri Shiavo case--and angered the conservative judiciary by calling for "retribution" against federal judges who are insufficiently right wing. DeLay, supposedly a champion of conservative values, has also been caught accepting high-priced golf trips from lobbyists against Congressional rules--plus paying $500,000 to his wife and family from campaign funds.<p>
Any opposition party worth the name should be having a field day, hammering the Republicans for their hypocrisy about morality and exposing Bush as a gofer for Wall Street.<p>
Instead, the latest manifesto of the Democratic Leadership Council (DLC)--the right-wing group that dominates the national party machine--advises the opposite. The DLC's method: make the Democrats indistinguishable from the Republicans on every issue that matters.<p>
"We must be willing to discard political strategies that may make us feel good, but that keep falling short," lecture DLC founder Al From and President Bruce Reed in the organization's magazine. Step one, according to From and Reed, is to out-Republican the Republicans on the "war on terror"--to "recapture the muscular progressive internationalism of Roosevelt, Truman and Kennedy and convince voters that national security is our first priority."<p>
From and Reed's manifesto echoes the language of an "open letter" to Democrats signed by 17 DLC members--led by an early 2008 presidential hopeful, Sen. Evan Bayh (D-Ind.)--which demands that the party "make clear to the American people that winning the war on jihadist extremism will be the Democratic Party's first priority this year and every year until the danger recedes."<p>
In other words, keep claiming that the Democrats would be more effective than Bush in fighting the "war on terror"--exactly the approach that lost John Kerry the presidential election. <p>
Not one of the difficulties the Republicans face today is the result of Democratic pressure. On the contrary, the Democrats abandoned all attempts to stop several prized items on the Republican agenda--the bankruptcy "reform" bill written by the credit card companies, limitations on lawsuits against corporations, oil drilling in the National Arctic Wildlife Refuge.<p>
And on the issue of abortion rights, the likes of Hillary Rodham Clinton are sounding the retreat, insisting that supporters of a women's right to choose should seek "common ground" with the right wing in reducing the number of abortions.<p>
This isn't opposition. It's collaboration. The Democrats are proving that they would rather lose elections and be consigned to permanent minority-party status in Washington than take a stand that threatens their cozy relationship with Corporate America. That's because both parties are part of a single political establishment that runs the system.<p>
Building an opposition--whether against the war, or to oppose Bush's class-war budget, or anything else--will depend on what people do at the grassroots. That's what the politicians of both parties understand--the pressure of people mobilized to fight back.<p>

