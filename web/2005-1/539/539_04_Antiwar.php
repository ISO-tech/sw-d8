<font face="Times New Roman, Times, serif" size="4">Impact of last year's election still felt</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Behind the antiwar movement's crisis</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 15, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">I RECENTLY attended a national antiwar teach-in at George Washington University in Washington, D.C. I think the event, sponsored by United for Peace and Justice (UFPJ) and the Institute for Policy Studies (IPS), demonstrated some important things about the antiwar movement right now. <p>
The first is that people were excited about the idea of a UFPJ antiwar teach-in. There were at least 300 people in the room, which was filled to capacity. The crowd included GWU students, folks from the D.C. left, staffers of non-governmental organizations and people who heard about it online. <p>
The second is that people like Gold Star Families member Celeste Zappala and Iraq Veterans Against the War (IVAW) member Patrick Resta, who were two of the panelists, are voices that need to be heard. They are very mad at this administration and very articulate about why the only solution to the horror waged by our government in Iraq is to bring the troops home now. <p>
However, there was a big problem in this meeting. Several times, panelists like Phyllis Bennis of IPS mentioned the big antiwar demonstrations before the war started. And their questions--really the main question of the evening--centered around the idea of "where is the outrage" now? How can we revitalize the movement?<p>
We were told by Rev. Osagyefo Uhuru Sekou of UFPJ--several times--to be Christians. We were told to come up with a more compelling agenda by Naomi Klein. Mostly, we were told to blame ourselves. <p>
Bennis said that some "people" aren't won to the idea of withdrawing U.S. troops now because some "people" support internationalizing the occupation instead. Of course! That was the idea hammered into our brains by the supposed presidential candidate of the antiwar movement.<p>
It is an outrage that leading members of UFPJ feel they can spend a year sitting on their hands in favor of the racist, you-break-it-you-bought-it, don't-cut-and-run, scion of U.S. imperialism--John Kerry--and then, after the election, set up their platform again and blame ordinary Americans for not being antiwar activists. <p>
This is part and parcel of the overall effort of the U.S. left not to deal with the disaster that was the Kerry campaign, but instead to demand that we all move further to the right because that's "where Americans are at." This is also, by the way, why several panelists deliberately misrepresented and denounced one audience member's question about supporting the right of Iraqis to resist the U.S. occupation.<p>
The antiwar movement does have, and has always had, a compelling agenda: it's to <i>bring the troops home now</i>! But for most of the last year, our movement has not raised that demand--our movement has hardly existed--because "our candidate" was pro-war and the party of "our opposition" is not an opposition. <p>
We cannot pretend that didn't happen. And that means there actually is something specific we need to do as a movement. We need to pledge that we will never follow the Democratic Party again. <br>
<b>Ben Dalbey, </b>Washington, D.C.<p>

