<font face="Times New Roman, Times, serif" size="5"><b>USWA holds line at Cognis</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Kerl</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 15, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">CINCINNATI--The multinational chemical giant Cognis Corp. is out to break the striking union at their plant here. In recent negotiations with United Steelworkers of America Local 14340, Cognis has refused to accept any contract language that protects the jobs of the 270 workers who walked off the job in early February to demand respect and job security.<p>
Cognis is dominant in the chemical industry, operating in 30 countries and privately owned. The Cincinnati plant manufactures oleochemicals for use in various consumer products. In 2002, the plant ranked among the worst polluters in the U.S., according to the Environmental Defense Fund. <p>
The struggle for job security arose when Cognis developed plans to split off 70 percent of the Cincinnati facility into a separate legal entity known as limited liability comapny. The result would be a loss of nearly 200 union jobs at the plant.<p>
This is only the most recent corporate restructuring scheme. Cognis itself began in 1999 as a split off from the German manufacturer Henkel Corporation. At that time, more than 400 workers operated the Cincinnati plant. Now, half the workers produce nearly 60 million pounds per month of the fatty acids, esters and surfactants. But Cognis wants to cut the workforce even more, especially good-paying union jobs. "When I was hired 29 years ago, you got a job and had a job for life," one striker told <i>Socialist Worker. </i>"Today you wonder from week to week if you have a job," <p>
This trend can be seen in all of the factories surrounding the Cognis Plant. At the nearby Proctor &amp; Gamble plant, a two-tier wage system means jobs that used to pay $22 per hour now pay $16. At the Sun Chemical plant next door, workers staged a strike in 2004 to stop an assault on their health care plans.<p>
Cognis workers are vowing to win the fight. The strike was approved by 94 percent of the membership, and remains solid after nine weeks. Local unions are donating resources to aid the strikers and activists are expressing solidarity. The fight for decent jobs at Cognis is our fight also.<p>
