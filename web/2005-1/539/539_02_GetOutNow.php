<font face="Times New Roman, Times, serif" size="4">Iraqis send a message to Washington:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Get out now!</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 15, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">CHANTS OF "No to America. No, no to occupation" filled Baghdad's Firdos Square last weekend as some 300,000 Iraqis protested on the second anniversary of the U.S. takeover of Iraq.<p>
Two years ago, Firdos Square became one of the Bush administration's favorite images when U.S. soldiers, surrounded by a hundred or so Iraqis, pulled down a statue of Saddam Hussein. Last year, U.S. troops sealed off the square with razor wire to prevent demonstrations. <p>
But this year, masses of people turned out to protest the U.S. occupation--and spilled out of the square and into the surrounding streets. Demonstrators toppled effigies of George W. Bush, British Prime Minister Tony Blair and Saddam Hussein--all dressed in red Iraqi prison jumpsuits.<p>
The rally, called by radical Shiite cleric Moktada al-Sadr, is the largest demonstration since U.S. troops invaded two years ago. Thousands of people traveled by bus from southern cities, staying overnight in the homes of residents in Baghdad's Sadr City--a stronghold of support for al-Sadr.<p>
"The American people need to know that they can't suppress us any more, even with all their strength and power," Mohammed Salih Khalaf, a 54-year-old day laborer from Sadr City, told the <i>Los Angeles Times</i>. Protester Ali Feleih Hassan told the Associated Press, "I do not accept having occupation forces in my country. No one accepts this. I want them out. They have been here for two years, and now they have to set a timetable for their withdrawal." <p>
According to Iraq expert Juan Cole: "[T]he crowds in downtown Baghdad protesting the U.S. troop presence in the country may have been as large as 300,000. If it were even half that, these would be the largest popular demonstrations in Iraq since 1958 [when mass demonstrations paved the way for the overthrow of the monarchy]. To any extent that they show popular sentiment shifting in Shiite areas to Moktada al-Sadr's position on the American presence, they would indicate that he is winning politically, even though the U.S. defeated his militia militarily."<p>
While the protest was largely made up of Shiites, Sunni Muslims joined in the demonstration--and also protested at large rallies in Ramadi and Najaf.<p>
U.S. military officials claimed credit even for the demonstration against them. "That you were able to see a group of people exercising their right to free speech is all part of what we're here for," Lt. Col. Steven Boylon told the <i>Los Angeles Times</i>.<p>
In fact, U.S. officials have begun talking about having "turned a corner" in Iraq. "We're on track," Gen. Richard Myers, chair of the Joint Chiefs of Staff, told the <i>New York Times</i>, suggesting that the Pentagon might be able to plan for significant troop reductions by early next year.<p>
But according to the <i>Times</i>, the "significant" reduction would still leave more than 100,000 U.S. troops occupying Iraq.<p>
For ordinary Iraqis, the U.S. presence means more death, destruction and humiliation. Like 60-year-old Salima al-Batawi and her daughter Aliya, who were blindfolded, handcuffed and driven away from their home by U.S. forces earlier this month. <p>
The women had committed no crime. But U.S. forces decided that they would be convenient pawns to force a male relative active in the resistance to surrender. "Be a man, Muhammad Mukhlif, and give yourself up, and then we will release your sisters," a note left on the gate of the women's home reportedly read. "Otherwise they will spend a long time in detention."<p>
After two years of occupation, the truth is plain to see: there will be no freedom for Iraqis as long as the U.S. stays in Iraq.<p>

