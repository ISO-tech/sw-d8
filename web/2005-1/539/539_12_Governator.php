<font face="Times New Roman, Times, serif" size="5"><b>Resistance builds to Governator's attack</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Michael Chase</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 15, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">"CALIFORNIA IS not for sale!" That was the message that as many as 2,000 protesters brought to a demonstration outside Gov. Arnold Schwarzenegger's $1,000-a-plate fundraiser at the Ritz Carlton hotel in San Francisco on April 5.<p>
Nurses, teachers, firefighters, health care workers and other union members crowded the street along with their supporters to demand an end to the Governator's war against workers and the poor.<p>
Some protesters donned Arnold masks and chanted "Shame on you!" at Schwarzenegger's affluent dinner guests. Other activists even booked a room in the hotel, where they unfurled an anti-Schwarzenegger banner out the window. An airplane trailed a banner overhead that read, "Arnold, California is not for sale!"<p>
Inside, Schwarzenegger raised money to finance ballot measures that he hopes to qualify for a special election, held this fall. Schwarzenegger is proposing the special election to press forward with his right-wing agenda at a time when the state is drowning in red ink.<p>
Last spring, Schwarzenegger said that he would honor Proposition 98, which guarantees minimum funding for schools, but he reneged. This is just one of Schwarzenegger's many attacks on teachers, including his plan to base teachers' salaries on "merit" and require that teachers have 10 consecutive years of satisfactory evaluations--based mostly on student test scores--to achieve tenure.<p>
Arrogance and contempt for working people have been the defining characteristics of Schwarzenegger's administration. When asked at a recent appearance how he would respond to ongoing protests by nurses, the governor said, "I'll kick their butts." "It's a sign of Schwarzenegger's autocratic nature," said Rose Ann DeMoro, executive director of the California Nurses Association (CNA).<p>
The CNA has been tenacious in pursuing the governor, dogging him at film premieres, parties and fundraisers across the country. Its persistence paid off last month, when a California court rejected Schwarzenegger's attempt to block the state from implementing minimum nurse-to-patient ratios.<p>
Another victory came the day after the protest, when Schwarzenegger retreated from his plans to privatize all of California's public pensions. This plan, like George W. Bush's scheme to "reform" Social Security, would be a huge gift to big business, while leaving workers subject to the chaos of the stock market in their "golden years."<p>
Schwarzenegger's agenda is a state version of the national trend toward privatization and free-market madness that will only lead to further economic hardships for poor and working people. The protests against Schwarzenegger are just beginning to show what can be accomplished if we stand up against the right wing's agenda.<p>

