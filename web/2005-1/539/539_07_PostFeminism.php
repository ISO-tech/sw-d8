<font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Post-feminism" or plain old sexism</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 15, 2005 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">"POST-FEMINISM" is the phrase <i>du jour </i>among mainstream political pundits, often accompanied by derogatory comments toward anyone who continues to complain that women lack equality.<p>
U.S. society has entered a "post-feminist" phase, a level playing field, according to this group of commentators. Women's oppression belongs to a bygone era, and ideas of women's liberation are merely outdated relics of that era. If anything, these pundits inform us, the women's movement of the 1960s went too far and now must be reigned in. This is the typical rationale for curbing abortion rights.<p>
But more insidious is the repeated accusation that feminists have created a "victim" consciousness among women by inflating statistics to exaggerate problems such as rape and sexual objectification. Without evidence, these claims have gained the status of conventional wisdom through sheer repetition.<p>
Syndicated columnist Kathleen Parker, for example, regularly ridicules the problem of date rape, with comments such as: "The biggest myth that won't die is that one of four college women is raped on campuses each year...If 25 percent of Daddy's little girls were being sexually assaulted at college, there wouldn't be any girls on campus."<p>
The figure Parker holds in such contempt is based on a study conducted in the 1980s by <i>Ms. Magazine,</i> surveying 6,000 students at 32 colleges which found that one in four college women had been a victim of rape or attempted rape--a figure now accepted even by the U.S. Department of Justice. Furthermore, less than one in every three rapes and sexual assaults were reported to law enforcement officials in 1996.<p>
Date rape is not a figment of the imaginations of "hysterical" feminists, but an indisputable reality for women in the U.S. Nevertheless, in the age of "post-feminism," demands for women's rights are deemed a colossal act of self-indulgence.<p>
Those who are offended by images of women in popular culture must be either pro-censorship prudes or outmoded feminists born without a sense of humor. After all, who but the most bitter feminist could fail to be amused by the plot line of Pamela Anderson's new Fox television show, which finds the former <i>Baywatch</i> babe starring as a busty bimbo working in a bookstore? (And the show's called <i>Stacked!</i>)<p>
Those who are irritated by the show's promotional ad (Anderson posing suggestively atop a pile of books, as her silicone-enhanced breasts defy the force of gravity) might be jarred by beauty standards and sexual ideals manufactured on Wall Street and in Hollywood yet absorbed by women throughout society.<p>
"Manufactured" is no exaggeration, given that 9.2 million cosmetic surgery procedures, from breast augmentation to Botox injections, were performed in 2004--mainly on women--according to the American Society of Plastic Surgeons, up 24 percent from 2000. Ironically, this rise in cosmetic surgery coincides with a 10 percent decline in medically necessary reconstructive surgery procedures in 2004 alone, due to declining insurance coverage.<p>
The beauty ideal, moreover, has changed drastically from the buxom Marilyn Monroe (size 12-14) to today's undernourished supermodel (size 2-4). A woman between the ages of 18 and 34 today has just a 1 percent chance of being as thin as a supermodel.<p>
As modeling executive Jennifer Venditti explained in a 2001 <i>Cosmopolitan </i>article, "The smaller the sample, the better [the clothing] drapes...It's almost like the body is not present." This can be a problem for human beings. Yet this image permeates popular culture with unrealistic images that negatively affect self-esteem among females from a very young age.<p>
Thanks to these airbrushed beauty standards, 80 percent of American women are dissatisfied with their appearance, according to the National Eating Disorder Association, contributing to the incidence of anorexia and bulimia currently suffered by up to 10 million women in the U.S. By the age of 10, 80 percent of girls in the U.S. have already tried to diet to lose weight.<p>
"Post-feminism" is a term brimming with hypocrisy--promoted primarily by those who oppose women's rights--turning women's bodies over to the control and judgment of others. The need for women's liberation is far from antiquated--it is, in fact, thoroughly modern.<p>

