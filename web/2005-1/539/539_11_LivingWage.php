<font face="Times New Roman, Times, serif" size="4">Students demand Wash U. pay a living wage</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"We're here to stay"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 15, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">ST. LOUIS--Students at Washington University have a message for their administration: Campus workers deserve a living wage--and we'll sit in until they get one.<p>
On April 4, a group of activists from the school's Student Worker Alliance (SWA) marched into the admissions office with signs, petitions and fliers--and declared that they wouldn't be leaving until the administration agreed to pay campus workers a living wage.<p>
Though there are 15 permanent sitters, anywhere from 20 to 40 students have been occupying the admissions office at any given time. Students have held daily solidarity rallies in the quad outside the admissions office windows and have even begun a small "tent city" outside as well.<p>
"We are here to stay," Joe Thomas, one of the sit-in organizers, told the <i>St. Louis Post Dispatch</i>. "We will miss classes if necessary. We'll even go to jail. We will be here 24 hours a day, seven days a week, until the chancellor decides it's better to pay his employees a living wage than to deal with us for another day."<p>
After months of SWA demands that the university adopt a code of conduct, including a living wage and card-check union recognition, a university taskforce recommended in May 2004 that Chancellor Mark Wrighton accept a number of the proposals, including the living wage component. Currently, campus workers at Washington University make approximately $8 an hour without benefits--a far cry from the living wage standard set last year by the St. Louis Board of Aldermen of $9.79 an hour with full benefits, or $12.15 an hour without benefits. <p>
Yet on October 1, the chancellor sent an e-mail to the entire student body saying that he had accepted all of the taskforce's proposals--<i>except</i> the living wage. <p>
"He said he wouldn't be a good steward of the money the university got from tuition-paying students if he didn't ensure that the workers on campus were [paid low wages], which I thought was interesting, seeing as how the university has always taken the moral high ground and always wants to be seen as an ethical institution," Ojiugo Uzoma, a senior and one of the cofounders of the SWA, told <i>Socialist Worker</i>.<p>
One economist has estimated that the maximum cost of implementing a living wage policy for all contracted workers at Washington University would be $2.4 million--a pittance, given the university's <i>$3.6 billion</i> endowment. <p>
Inspired by the recent success of a nine-day student hunger strike that won a living wage for campus workers at Georgetown University, the SWA decided to turn the heat up on the administration. As <i>Socialist Worker </i>went to press, a university council meeting was being held to discuss the code of conduct--and student were promising to continue the sit-in as long as it takes.<p>
