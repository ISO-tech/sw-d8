<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">April 15, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>General Cable Corporation</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>by Brian Chidester</b></font> <p>

<font face="Times New Roman, Times, serif" size="3">LINCOLN, R.I.--Members of United Steelworkers of America Local 4543 ended their two-week strike on April 4 with a contract that marks a step backward. <p>
The four-year contract, ratified by a vote of 111 to 68, includes a co-pay on healthcare of $5 per week for single plans and $13.50 for families in the third year, and 7 percent of health costs in the fourth year. The wage agreement includes a $1,000 bonus in the first year and raises of 2 percent, 2.5 percent and 2.7 percent in the following years. While the co-pay amounts are actually smaller than the company had first demanded, the raises are lower. <p>
According to Carlos Lomba Jr., treasurer of Local 4543, the contract isn't as good as the company's previous offer made two days into the strike, which workers rejected overwhelmingly. However, the company was bringing in scabs and threatening to move machinery and operations to other plants, leading workers to believe the strike would be lost. "We did get the best contract overall out of all the [15] other plants," Lomba, who has been at General Cable for 21 years, said. However, "it had to be the worst contract we've had since I've been there." <p>
Workers' militancy was dampened by the sense that everyone everywhere is paying some part of their health insurance, and that it was only a matter of time for Local 4543 to begin doing so--a notion reinforced by the International leadership of the USWA. Local 4543 is aware of the other union contracts, but has never tried to coordinate its efforts with the other unions--and the lack of solidarity put the local at a disadvantage.<p>
Two important lessons can be drawn from this strike. First, 200 workers can't take on a transnational corporation alone. They need the active solidarity of other workers, especially those at the company's other plants. Second, strikes have to be able to shut down production--which means organizing picket lines that can keep out scabs.<p>
This kind of action--in defiance of the law, if need be--will be key for any union that wants to beat such a determined and profitable boss. <p>

