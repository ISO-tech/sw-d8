<font face="Times New Roman, Times, serif" size="5"><b>Putting the blame on teachers and kids</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 15, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">YOU KNOW that a school system has its priorities backwards when a high school principal is arrested by a police officer. But that's exactly what happened recently in Bronx Guild High School in New York City.<p>
The principal, Michael Soguero, allegedly tried to prevent the arrest of one of his students, stepping in between the cop and a 16-year-old girl. This was too much for the police officer, who immediately arrested Soguero, claiming that he was shoved.<p>
The incident was about much more than an arrest. It reveals how New York City's response to safety issues in schools has been the criminalization of students, which hasn't addressed the root problem: overcrowded buildings and classrooms.<p>
Last year, Mayor Michael Bloomberg announced a plan to target the 12 most troubled schools in the city--and flood them with police and extra security agents. The school I teach in, Christopher Columbus, was labeled one of these "impact schools" after a single incident that was a direct result of overcrowding (our building has been at over 140 percent capacity for four years). <p>
At first, there was a slew of extra cops--and not a few students dragged out of classrooms in handcuffs. But since then, the extra "support" has disappeared, to be replaced by inspections by the local superintendent, who blames teachers. Teachers were told recently that their "instruction sucks."<p>
As Nick Licari, the chapter leader at Norman Thomas High School, put it: "They can't immediately deal with the problem of overcrowding, so they address the problem of safety." Norman Thomas teachers threatened to walk out several months ago over safety concerns in their building.<p>
Safety is an important issue in educational workplaces--but the incident at Bronx Guild shows how more cops is exactly the reverse of what needs to happen. As Soguero said days before his arrest, a flood of cops just leads "increased hostility between young people and staff in the building," which soon fades as the Department of Education turns its attention elsewhere and falls back on its old standby: blaming the teachers.<p>
Education under capitalism has always been about throwing away one section of youth, condemning them to low-paying jobs, the military or jail. We need a system that has money for more classrooms and well-paid teachers for all students, not just more police.<br>
<b>Peter Lamphere, </b>New York City<p>

