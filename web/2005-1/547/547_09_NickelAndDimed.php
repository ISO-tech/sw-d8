<font face="Times New Roman, Times, serif" size="5"><b>Working poor take center stage</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Cindy Beringer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 10, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Nickel and Dimed, </b></i>a play by Joan Holden, adapted from the book by Barbara Ehrenreich.</font><p>

<font face="Times New Roman, Times, serif" size="3">JOAN HOLDEN'S adaptation of Barbara Ehrenreich's best-selling book about the working poor makes for powerful theater.<p>
As a journalistic experiment, Ehrenreich set out to see if she could survive in a series of low-paying jobs for her 2001 book <i>Nickel and Dimed: On (Not) Getting By in America</i>.<p>
Each role gives human faces to workers who must endure the thousand slings and arrows of outrageous exploitation. The thinly veiled names of the corporations that employ the characters do not protect the guilty.<p>
Ehrenreich's first job is at Kenny's Restaurant. Then she takes a second job cleaning hotels in order to survive. Her jobs include a maid service, a nursing home and a retail store called Mall-Mart.<p>
All the workers must endure the indignities of drug and employment tests and insensitive managers. Finding affordable housing in many places is next to impossible. Mothers of young children must make horrendous choices between keeping their jobs and holding Child Protective Services at bay.<p>
An arthritic hotel maid munches eagerly on a package of hot dog buns for lunch. A pregnant maid service employee, who supports her unemployed husband and his mother, sprains her knee on the job but refuses to tell her employer and collect workers comp because she is so desperate to keep her job.<p>
Each vignette is heart-wrenching and so very real, yet I left the theater wanting to scream.<p>
In New River Valley, Vt., the play was sponsored by a living-wage campaign. Tickets were free with small donations requested. In Los Angeles, theater-goers paid $45 for the play, valet parking and dining al fresco.<p>
In Austin, Texas, where I saw the play, the production was sponsored by the Charles Schwab investment company and a huge law firm. Reviewers in local papers ignored the play's clear message and focused on the truly magnificent life-sized posters of Austin women in dead-end jobs and the photographer who took them. <p>
Ehrenreich's promise to "tell your story" must have been small comfort to the barely surviving workers she confides in. The sick system defined in the drama is ripe for Marxist analysis and cries out for change.<p>
Instead, near the end, an actor playing a male manager addresses the audience with predictable capitalist rhetoric: "You look like a well-heeled bunch. Would you be willing to wash your own car or pay a third more at a restaurant?" The fault, he assures us, is "all in the numbers." In other words, change is impossible.<p>
A note in the program from the artistic director acknowledges that the women in the photos are "our near neighbors...We honor them, we applaud their labor, and we celebrate their contribution to our lives." But don't even think about forming a union, earning a living wage or asking for benefits.<p>
See this play if it comes to your area, but turn it into a campaign for radical change!

