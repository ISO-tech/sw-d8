<font face="Times New Roman, Times, serif" size="4"><b>A sick health care system</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I RECENTLY  had to go to the emergency ward at my local hospital since I was having breathing problems due to allergies. There is medication that would control my breathing. I even have "decent" health insurance. <p>
So why am I going to the emergency room? Because I gave the medication to my sister, who just lost her job along with her health insurance, and who also has allergies that this medicine controls. <p>
So why didn't I just go to the local drug store and get more medicine? Because my health insurance company said that I should still have medication left for another two weeks&#8212;and this is even <i>after</i> my doctor gave me another prescription. We <i>clearly</i> live in a sick society when a health insurance provider is deciding on what dose a person needs over the person's own doctor!<br>
<b>Anonymous,</b> From the Internet<p>
