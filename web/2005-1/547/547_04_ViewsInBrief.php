<p><a name="Exit"></a><font face="Times New Roman, Times, serif" size="4"><b>A reasonable exit strategy </b></font><p>

<font face="Times New Roman, Times, serif" size="3">U.S. REP. Lynn Woolsey's Resolution 35 presents a reasonable exit strategy from Iraq. It calls on the president and Congress to create and execute plans to withdraw United States troops and replace them with security forces from Iraq, neighboring states, the United Nations and the Arab League. Further, it calls for the reconstruction of Iraq and for returning to Iraqis control over their country.<p>
The United States does not have enough boots on the ground to maintain security and those who are there are getting worn out. Recruitment of new troops is way down, and a draft would be political suicide. <p>
"Staying the course" means spending $100 billion a year and depending on the development of an Iraqi police force that takes the job because it is the only one available. Further, the Iraqi police are reluctant to shoot their own people because they see the insurgents as patriots and the occupation as the enemy. <p>
Polls now show that most Americans believe that the war in Iraq was a mistake. Resolution 35 gives us a chance to improve our image around the world, reduce the threat of terrorism and support our troops by bringing them home.<br>
<b>Jerry Bitts,</b> Las Vegas<p>

