<p><a name="Stoploss"></a><font face="Times New Roman, Times, serif" size="4"><b>The insanity of "stop-loss"</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I HAVE been puzzling with a question for some time now, which is a subject I have not seen examined the media. Inasmuch as there is no draft or compulsory military service, how can refusal to go to war become the subject for a trial? Even a charge of "desertion" is absurd under these circumstances. <p>
Unless I am completely wrong, when an American signs up for service, he/she enters into a "contract of employment." As such, if you do not fulfill that contract, the worst-case scenario should be "breach of contract" which is a civil matter, nothing more nothing less. In that connection, the ludicrous "stop-loss" invention is not an enforceable tactic and downright illegal. <p>
No employer can invoke a stop-loss clause on an employee&#8212;the entire idea is so ridiculous that it has to be a bad joke!<p>
This administration is so completely illegitimate and comes up with the most crazed rules and ideas, which they use our tax money to implement and enforce. I don't know if they have thought about selling our Constitution to the Iraqis, them being hard-pressed time-wise to write their own, because we certainly do not seem to have any use for it&#8212;nor the Bill of Rights and other adherent documents which our country is built upon.<br>
<b>Hanna K. Jaeckel,</b> Bridgeport, Conn.<p>
