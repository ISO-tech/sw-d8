<p><a name="Sith"></a><font face="Times New Roman, Times, serif" size="4"><b>Missed the force of <i>Sith</i>, <i>SW </i>did</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IN HIS review of <i>Revenge of the Sith,</i> Joe Allen does a fine job of setting up a straw man and then striking him down with a mighty swing of his lightsaber&#8230;er, pen ("Evil Empire USA," May 27).<p>
If, as Joe asserts, George Lucas really "hopes his film will have the same impact as Michael Moore's <i>Fahrenheit 9/11</i>," he certainly isn't acting like it. In fact, while he is openly critical of Bush and the war on Iraq, Lucas has said the film was written before 9/11, and that the similarities with Bush are only a happy coincidence.<p>
True, there are similarities in <i>Sith</i> between Darth Vader and Bush. Joe left out one of the best&#8212;when Natalie Portman notes, "This is how liberty dies&#8212;to thunderous applause," as the Senate cheers on the Emperor's rise during a wartime crisis.<p>
The original <i>Star Wars</i> also had similarities with Vietnam in its story of a rag-tag group of rebels taking down the technologically superior Empire. Should we criticize it for not having a greater impact on political consciousness?<p>
Exciting Hollywood blockbusters that take a few pot shots at America's warmongers are fine by me, especially considering most of the garbage out there. And while Joe is right about some of the horrendous dialogue, that doesn't stop <i>Sith</i> from being the best Star Wars film since <i>The Empire Strikes Back</i>.<p>
Socialists should critique popular movies based on their actual merits and shortcomings&#8212;not based on whether they meet some political goals which we impose on them. If Lucas is in fact trying to remold himself into a rich-man's Michael Moore, then he is failing miserably. If, on the other hand, he is just trying to make a damn good Star Wars movie, then he has succeeded.<br>
<b>Scott Johnson,</b> Oakland, Calif.<p>
