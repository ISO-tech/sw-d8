<font face="Times New Roman, Times, serif" size="5"><b>The truth about Pat Tillman</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">June 10, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">PATRICK TILLMAN is furious about the Army's lies and cover-ups surrounding the death of his famous son Pat.<p>
Pat Tillman gave up a lucrative salary with the Arizona Cardinals to join the Army after September 11. He was killed last April shortly after being sent to Afghanistan. <p>
The Army reported that Tillman had gone down fighting the enemy. He was awarded a posthumous Silver Star for bravery. Some time after a high-profile memorial service for Tillman, the Army had to admit that Tillman was killed in a friendly-fire incident. <p>
New information revealed recently in the <i>Washington Post </i>from field documents and witness statements gives more details, presenting a troubling and confusing account of the incident. It is clear, however, that military officials knew from the beginning that Tillman was killed by his fellow soldiers and withheld that information from his family for some time.<p>
"After it happened, all the people in positions of authority went out of their way to script this," Tillman's father told the <i>Post</i>. "They realized that their recruiting efforts were going to go to hell in a hand basket if the truth about his death got out."<p>
Tillman's mother is haunted by the stories of his death. "It makes you feel like you're losing your mind in a way," she said. "You imagine things...The truth may be painful, but it's the truth. You start to contrive all these scenarios that could have taken place because they just kept lying." She calls the Army's lies "disgusting," and blasts George Bush for running a taped memorial to her son before a Cardinals game last fall prior to the election.<p>
The Tillmans are sure that they will never know the truth about how their son died. "I think there's a lot more yet that we don't even know," said Mary Tillman.<p>
Amid the hype we were subjected to on Memorial Day, the voices of Patrick and Mary Tillman were especially necessary.<br>
<b>Cindy Beringer, </b>Austin, Texas

