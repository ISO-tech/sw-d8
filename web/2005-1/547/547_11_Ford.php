<font face="Times New Roman, Times, serif" size="5"><b>Ford-Visteon deal marks new defeat for UAW</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 10, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">THE RETREAT by the United Auto Workers (UAW) is turning into a full-scale rout. <p>
Union leaders backed a deal--ratified by members June 6--that will pave the way for massive cuts in wages for 17,500 workers at Visteon, the former parts division spun off from Ford in 2003. The deal allows Ford to take back 24 plants from Visteon--including 15 UAW-represented plants in the U.S.-- and place all but two in a holding company. That yet-to-be-named company will then sell the plant to third parties.<p>
This will enable Ford and Visteon to wriggle out of an agreement with the UAW, under which union members at Visteon would remain Ford workers and be eligible for full pension benefits. But now that they'll no longer be Visteon workers, they're certain to lose wages and benefits when new owners take over in the future.<p>
That's exactly what happened at a former Chrysler parts plant bought by Metaldyne, which gave high-seniority workers the option to transfer, but cut wages by $10 per hour for the rest.<p>
Workers reportedly voted nearly 90 percent in favor of the deal. This was shaped by two factors--the possibility of getting one of the 5,000 buyout packages that are promised before the 2007 contract, and the feeling that there's no alternative given the UAW leadership's unwillingness to fight concessions.<p>
In fact, UAW President Ron Gettelfinger continues to insist that the union won't agree to reopen its labor contracts with the Big Three automakers--yet union officials continue to do exactly that. The union already stood pat in March when Chrysler foisted higher health care costs on workers.<p>
Next, General Motors and its former parts division, Delphi, will seek similar concessions from the UAW. They've convened a closed meeting, exposed only when Delphi worker Gregg Shotwell, a member of UAW Local 2151 in Cooperstown, Mich., got wind of it and alerted the press. <p>
"There's been no 'call letter' to the Delphi locals notifying them of this meeting," Shotwell said, referring to the UAW procedure in which local representatives are summoned to bargaining-related meetings. Shotwell expects that the UAW will agree to allow Delphi to sell off plants, and cited a local example in Grand Rapids, Mich., where an old GM plant was transferred to Delphi and then sold off to Lear.<p>
New hires were paid less in a two-tier agreement; when the lower-wage workers were a near majority, the company got the UAW to agree to a $10 per hour wage cut across the board. Wages are so low in the plant--about $16 per hour--that they're close to what unionized cashiers make at the new Costco store in town. <p>
Looming behind these concessions is GM's demands for relief from the costs of retiree health care.<p>
The companies' new demands for concessions--and the UAW leadership's surrender--have renewed opposition activism. The UAW Solidarity Coalition expects to hold a meeting in September to strategize on ways to fight back. <p>

