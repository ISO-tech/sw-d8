<font face="Times New Roman, Times, serif" size="5"><b>Just your ordinary all-American family</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 10, 2005 | Page 9</font><p><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Family Guy,</b></i> created by Seth MacFarlane, written by Mike Henry, airs Sundays at 9 p.m. Eastern time on Fox.</font><p>

<font face="Times New Roman, Times, serif" size="3">NO ONE--not even the Pope--is spared from the searing satire dished out each episode on <i>Family Guy,</i> an animated TV sitcom tailored to amuse the most skeptical mind. <p>
<i>Family Guy </i>has something to say about virtually every social convention in America today--usually when the grossly dysfunctional Griffin family inadvertently breaks one or another social taboo and must suffer the dreaded consequences.<p>
The Griffins--Peter, his wife Lois, their three oddball children, and their talking dog Brian--want, more than anything else, to fit in with everyone else. The Griffins are trying to be just another stereotypical American family from the working-class suburb of Quahog, Rhode Island.<p>
They embrace "good old-fashioned family values" (spelled out in the show's theme song), spend most of their time parked in front of the TV (mesmerized even by the commercials), and regard family birthdays and Christian holidays as sacred events. <p>
Peter works on the assembly line at the Happy-Go-Lucky toy factory, carouses with his buddies at the "Drunken Clam" and wants his son to advance in the "Youth Scouts." Lois is a stay-at-home mom who, despite many obstacles, always puts her family first--but must supplement the family income by giving piano lessons. <p>
Teenage daughter Meg seeks, unsuccessfully, nothing more than to be liked by the popular crowd at high school, while adolescent son Chris is, well, a typical alienated adolescent--but who has, alas, been rejected by the Youth Scouts.<p>
Baby Stewie, granted, is not the average 1-year-old. While not yet toilet trained, he spends most of his time plotting his mother's assassination--when he is not pursuing his goal of "world domination."<p>
And Brian is certainly not the average family dog. His intellectual sophistication and personal depth surpasses anyone else in the household. As such, he usually sips a Martini and is prone to bouts of depression.<p>
Things just never seem to work out for the Griffin family. Peter's outlandish behavior is often the reason.<p>
Peter, unfortunately, lacks the most basic elements of common sense and judgment, accepts every conventional prejudice and racial stereotype in existence, and has a penchant for lying. That is how, in one episode, Peter, after discovering one of his ancestors was African American, proceeds to alienate every Black person he encounters with his new knowledge.<p>
In another, Peter befriends a Mafia hit man, who in a fit of jealousy puts out a contract on Lois' life.<p>
Of course, the Griffins are also prone to just plain bad luck--as when Peter invites his boss from the Happy-Go-Lucky toy factory to dinner, and he ends up choking to death.<p>
But plot lines alone cannot do justice to <i>Family Guy.</i> Cameo appearances by William Shatner, Gwyneth Paltrow and the aforementioned Pope somehow fit into the Griffin family's myriad of misfortunes. The local news anchor team, "Tom and Diane," along with "Asian Reporter Tricia Takinawa," regularly distort the local TV news. <p>
Viewers armed with a sense of humor and a clear disgust for so-called American values will wholeheartedly enjoy the full-throttle irony of <i>Family Guy. </i>And to those out there who still believe that America is overrun with conservatism, it's worth noting that due to the unrelenting pressure from millions of loyal fans, <i>Family Guy</i> has been revived after cancellation in 2002.<p>
You can view new episodes on Fox Network's Sunday night lineup of irreverent comedy, right after <i>The Simpsons.</i> (This is appropriate, since Peter Griffin could easily be Homer Simpson's alter ego.) You can also see old episodes on the Cartoon Network's nightly "Adult Swim."<p>
You will probably feel like less of a social outcast after watching.

