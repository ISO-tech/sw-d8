<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">June 10, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>United Airlines</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><p>

<font face="Times New Roman, Times, serif" size="3">UNDER PRESSURE of a judge's threat to void union contracts, mechanics' and ramp workers' unions at United Airlines have agreed to $700 million in contract givebacks. The agreements put the total number of labor concessions at the company at about $3.5 billion since 2003.<p>
The Aircraft Mechanics Fraternal Association (AMFA) announced May 31 that members voted to ratify the deal, 2,701 to 1,867. About 1,094 failed to vote.<p>
The deal includes a 3.9 percent pay cut as part of $96 million in concessions. These givebacks come on top of a 14 percent cut under the mechanics' previous union, the International Association of Machinists (IAM), when a bankruptcy judge imposed the reductions in 2003. The mechanics voted to oust the IAM later that year and replace them with AMFA.<p>
AMFA, unlike the IAM, opened up negotiations, but brought back a concessionary contract late last year that was rejected by the rank and file. In the meantime, United reached a deal with the government's Pension Benefit Guaranty Corp. to dump its retirement plans--thereby drastically cutting benefits for most retirees.<p>
The company earlier reached a deal in January with the Airline Flight Attendants, affiliated with the Communications Workers of America, in which union leaders successfully sought a 9.5 percent pay cut and other givebacks.<p>
For its part, the IAM was not expected to announce details of its tentative agreement until mid-June--but company officials signaled that management had obtained most or all of the $175 million in givebacks that it wanted.<p>
 The agreement will only increase pressure on the unions to make givebacks at other airlines--often in bankruptcy court--that have already extracted billions in concessions in the last four years. The use of the bankruptcy proceedings to break union power has to be challenged if airline labor has any chance of regaining its strength.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Chicago teachers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jesse Sharkey, CTU delegate</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--Late last week, the Chicago Public Schools (CPS) announced $25 million of cuts and layoffs, the third such announcement in the past month.<p>
Just two weeks earlier, CPS had announced the firings of 1,096 non-tenured teachers, a move which school officials justified on the basis that it would improve the quality of the city's teaching staff. However, even the CPS board now admits that some of the firings were vindictive--targeting teachers with superior ratings who were union activists, as well as those who had criticized their principals. <p>
In April, the district cut some 800 teaching jobs, resulting in increased class sizes and cuts to programs.<p>
Chicago Schools CEO Arne Duncan blamed state leaders for the crisis. "Given all the progress we've made, it's infuriating," said Duncan. "The state ought to be ashamed of itself."<p>
But blame for Chicago's budget mess can be shared by Mayor Richard Daley whose tax-free development zones have cost the city $110 million a year. At the state level, Democratic Gov. Rod Blagojevich and the Democrat-controlled House and Senate cut a deal to balance the state's budget by cutting $1.8 billion from the state workers pension fund.<p>
In the face of these announcements, Chicago Teachers Union President Marilyn Stewart sent a letter to all 36,000 members blaming the firings on previous President Deborah Lynch. Lynch's supporters fired back at the House of Delegates meeting in June, attempting to block the union's budget and accusing the current leadership of lying about the contract provisions that left the union open to the mass firings. <p>
After Lynch, who is not currently a delegate, gave a speech on the floor of the meeting, someone in the CTU leadership called the police and had her arrested for not leaving fast enough. After witnessing uniformed police carry off the former CTU president, the meeting descended into pandemonium.<p>
No word about how to address the problems faced by the union came before business was adjourned for the summer.<p>

