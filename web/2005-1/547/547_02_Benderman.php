<font face="Times New Roman, Times, serif" size="5"><b>Benderman faces new trial</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 10, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">SGT. KEVIN Benderman will again face a general court-martial, but the investigating officer has recommended that the Army dismiss some of the charges against him.<p>
Last month, as the Army was set to begin Benderman's trial, a military judge ruled that a previous investigating officer had been biased against Kevin during the military's equivalent of a pretrial hearing. The judge ordered that Kevin had a right to a new investigating officer and a new pre-trial hearing.<p>
Seemingly in retaliation, the Army added larceny charges to the existing charges against Kevin of desertion and missing a deployment--because, it claimed, Kevin improperly received combat pay. But the Army itself admits that its own accounting error was at fault--and that more than 100 soldiers at Kevin's base alone were affected.<p>
The new investigating officer strongly recommended dropping the larceny charge and advocated qualifying the desertion charge. Unfortunately, the Army didn't downgrade the proceedings against Kevin from a general to a special court-martial. A special court-martial carries a maximum sentence of one year per charge. In a general court-martial, Kevin could face a sentence of 10 years or more.<p>
"At the most, the government can prove Sgt. Benderman went home for the weekend," said William Cassara, Kevin's attorney. "When you get past all the histrionics, this case is about--at most--a three-day [absent without leave]." The date for the new court martial is expected in the coming week.<p>
Kevin Benderman is one of a number of soldiers who have resisted the U.S. war machine from the inside. They need the full support of the antiwar movement.<p>

