<font face="Times New Roman, Times, serif" size="5"><b>U.S. resister rejected for asylum</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 1, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">THE CANADIAN government has denied political asylum to Jeremy Hinzman, a U.S. Army paratrooper who fled to Canada to avoid serving in Iraq. Hinzman is one of eight U.S. service members who have fled the U.S. and petitioned the Canadian government for asylum, saying that they would face persecution for refusing to fight if forced to return to the U.S.<p>
Hinzman had originally applied for conscientious objector status, but the U.S. military ignored his application and sent him to Afghanistan in 2002. The following year, Jeremy re-filed his request and was again rejected.<p>
In 2004, after receiving word that his unit would be redeployed to Iraq, he traveled to Canada in the middle of the night, with his wife and their infant son. "Perhaps I made a mistake by enlisting in the Army, but the U.S. is putting the lives of its soldiers in jeopardy in order to the line the pockets of big money," Jeremy explained. "I will not get blood on my hands or put my life in danger for such an endeavor."<p>
But according to Immigration and Refugee Board member Brian Goodman, who wrote the ruling that denies asylum for Jeremy, Hinzman doesn't meet the criteria for conscientious objector status--because he offered to serve out the rest of his term of service in a non-combat role.<p>
Goodman also wrote that while Hinzman might face some discrimination if he returns to the U.S., "the harm is not serious." Try telling that to war resister Camilo Mejia--who spent seven months in a military prison for his refusal to redeploy to Iraq.<p>
Hinzman's attorney is appealing the ruling. But if he is forced back to the U.S., Jeremy will likely face charges of desertion--which carries a penalty of up to five years in jail, and in extreme cases, a death sentence. With the Army missing both its February and March recruiting goals, the ruling is a victory for the U.S. military, which is looking to send a hard-line message to resisters like Hinzman.<p>
