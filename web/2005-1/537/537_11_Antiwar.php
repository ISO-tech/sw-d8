<font face="Times New Roman, Times, serif" size="5"><b>No to war and occupation</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jen Roesch</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 1, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--About 200 people gathered March 26 at the Broadway Presbyterian Church for a forum on "Challenging Bush's empire: Our right to resist." The forum featured activists who have spoken out and organized against Bush's war abroad and its manifestations at home. <p>
Kim Rosario, an activist with the Out Now coalition, has a son currently deployed in Iraq. "My greatest fear is not that my son will be killed but that he will kill and come home traumatized--that he will kill an innocent Iraqi," she told the audience. She attended the panel even though her son was home on leave and scheduled to return to Iraq the following day. <p>
Other speakers included Lubna Hammad, a member of Stop McCarthyism at Columbia, a group formed to protest the witch-hunt against Arab and Muslim professors who are critical of Israel and U.S. foreign policy; Ewa Jasiewicz, a human rights activist who has worked with unionized oil workers in occupied Basra; Brenda Stokely, a founding member of NYC Labor Against War; and Hadas Thier, one of the City College students arrested at a counter-recruitment protest and a member of the International Socialist Organization.<p>
"The truth is that our movement is working," Thier told the crowd. "It's tapping into a widespread sentiment against the war, and widespread sentiment against being used as cannon fodder for a war in which young working-class kids are asked to sacrifice our lives for a war which only benefits the Halliburtons, the oil companies and American empire."<p>
The evening was both sober and inspiring. While many of the speakers have come under attack for daring to stand up, they have also held firm to their principles and given others the confidence to come out and support them--showing the potential for rebuilding the antiwar movement on a clearer and stronger basis.<p>
In <b>Washington, D.C., </b>about 30 members of the University of the District of Columbia (UDC) community turned out to protest recruiters from the Judge Advocate General's (JAG) office at the UDC David A. Clarke School of Law on March 24.<p>
JAG, the legal arm of the military, was allowed on campus because of the Solomon Amendment, which is a federal law that strips a university of any federal funding if it denies JAG from recruiting because of a conflicting school or state policy that prevents employers from discriminating on the basis of sexual orientation. During this successful first protest, students and faculty members stood and sat in the foyer of the administrative building, holding signs against the military's "don't ask, don't tell" policy, and handing out fliers on the military's discrimination and the war on Iraq.<p>
In <b>Lambertville, N.J., </b>some 365 people gathered for a rally endorsed by 20 organizations on a rainy March 20--the largest demonstration in this area since exactly two years ago when the U.S. launched Operation Shock and Awe. Several speakers and musicians addressed various issues connected to the occupation of Iraq. "Why were Americans shocked by the torture at Abu Graib when we have the same thing going on in prisons in the United States?" Manijeh Saba, a long-time human and women's rights activist originally from Iran, asked the crowd.<p>
A day earlier, on March 19, in addition to several large protests across the U.S. were many smaller ones. In <b>Columbus, Ohio, </b>more than 300 activists defied a chilly March rain to voice their opposition. In <b>Madison, Wis., </b>about 300 protesters marched on the ROTC recruiting center. <p>
In <b>Olympia, Wash., </b>more than 200 people turned out in a steady downpour, including Susan Livingston, whose brother died in Iraq on December 8, 2003. Livingston recalled meeting with President Bush at nearby Fort Lewis after her brother died. "He told my mom, 'I'm going to make sure your son didn't die in vain.' He was telling all the mothers that. A thousand coffins have come home since Joe's, and it's still in vain." <p>
<i>Leah Golshani, Brian Huseby, Stephanie Jung, Matt Pillischer and Ty Williams contributed to this report.</i> <p>


