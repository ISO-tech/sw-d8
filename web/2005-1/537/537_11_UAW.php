<font face="Times New Roman, Times, serif" size="4">Health care increases at Chrysler put pressure on UAW</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Big Three on the warpath</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 1, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">THE BIG Three automakers are taking aim at the United Auto Workers (UAW) as they try to push health care costs onto the backs of workers--but union leaders so far have put up no resistance.<p>
By forcing the costs onto the union, the companies are testing the waters for a full reopening of the 2003 contract to win far greater concessions from the UAW at a time when layoffs and previous givebacks have already taken a massive toll.<p>
Chrysler led the attack by invoking an obscure "side letter" in the 1982 and 1996 contracts that will allow the company to force workers in PPOs to pay annual deductibles from $100 to $1,000 for services previously available at no cost. Under the deal, about 35,000 workers will have to pay the new fees for the first time. Another 70,000 covered in fee-for-service plans won't be affected.<p>
The deal effectively re-opens the contract ratified in 2003 and will likely prompt General Motors and Ford to follow suit. GM reported that it paid $5.3 billion in health care costs this year. Ford paid more than $3 billion; Chrysler's owner, DaimlerChrysler, reported $2 billion in health care costs.<p>
The mounting health care costs come at a time when poor sales and sinking profits have led to major losses at GM, which anticipates an $850 million loss in the first quarter, the worst such total since 1992. The company's first target was the white-collar workforce, which will be cut by about 2,000 this year. The company also plans to lay off 2,800 workers at its Janesville, Wis., SUV assembly plant, one of the most profitable operations in the late 1990s but now plagued by declining sales.<p>
Meanwhile, the UAW has denied that they will agree to reopen the contract, which was done only once before, in 1979, to clear the way for a government bailout at Chrysler.<p>
Nevertheless, talks continue, even without a formal contract re-opener. "We're working and talking every day with the companies about health care and both sides are putting their heads together to see how we can solve the crisis," UAW Vice President Nate Gooden, who has responsibility for Chrysler, told reporters. "We realize we've got a crisis."<p>
For dissident rank-and-file UAW activists, such comments suggest that the UAW is preparing to agree to givebacks. "I'm concerned that there is any language in there whatsoever that allows the employees to modify any portion of the health care," said Jan Austin, financial secretary of UAW Local 594 which represents workers at GM's Pontiac, Mich., truck plant. "I am kind of concerned with [UAW leaders'] focus on health care," she said. "I don't think we should let our guard down on other issues."<p>
Those who challenge UAW leaders face harsh retaliation. Austin's husband, Gene, was recalled as plant chairman after UAW officials and GM management campaigned to discredit him by refusing to negotiate a local contract. A year later, there's still no deal.<p>
Meanwhile, UAW leaders' strategy has been to maintain wages and benefits for a shrinking number of assembly plant workers while accepting downsizing through outsourcing to parts plants.<p>
In the 2003 contract, the union broke with its tradition of equal pay for equal work, agreeing to wages as little as 50 percent of those of assembly plant workers. Last year, the UAW agreed to an even worse deal for two-tier pay at Caterpillar despite record profits at the heavy equipment company. The company also made a similar agreement with CNH, another heavy equipment maker, after a four-month strike.<p>
Meanwhile the UAW--with just half of its 1.5 million members in 1979--has been struggling to reverse a string of organizing failures at foreign-owned auto assembly plants. The union recently opened an organizing office to reach out to workers at a Nissan plant in Canton, Miss., and has an upcoming union recognition vote at Toyota's huge assembly complex in Georgetown, Ky. <p>
Jan Austin wondered whether the union can keep retreating in its Big Three strongholds and appeal to unorganized autoworkers. "The only things our union has been good at in recent years is protecting pensions and health care, and now they're failing to do that," she said.<p>

