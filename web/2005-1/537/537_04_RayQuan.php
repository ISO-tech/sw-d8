<font face="Arial, Helvetica, sans-serif" size="2"><b>OBITUARY: RAY QUAN</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>He strengthened everything we did</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 1, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">WE ARE shocked and deeply saddened by the recent death of Bay Area labor activist Ray Quan.<p>
We met Ray around 1997, just after we moved to San Francisco. Rebecca got a job working on a joint organizing campaign with SEIU Local 790 and 250. While there, Ray basically took Rebecca under his wing, giving her insight into union politics and sometimes fielding "help me" calls as many as three or four times a day. <p>
We both worked with Ray while organizing workers and workfare workers at San Francisco General Hospital. This was in the aftermath of "welfare reform," and San Francisco was forcing people receiving general assistance to work for slave wages at various city agencies. Ray was one of the first people to expose how the practice was a way for the city to bust the union by filling what would have been union jobs with people "working off" their benefits. Ahead of his time, Ray led a joint union-workfare workers committee to press the issue with the hospital and city. <p>
Ray was a staunch ally in the efforts of Eduardo and his colleagues to organize non-profits into SEIU Local 790. We worked on numerous struggles--the Bay Area Rapid Transit strike; contract fights of the International Longshore and Warehouse Union; Filipino airport workers' fight against anti-immigrant restrictions after September 11; and other labor struggles too many to mention; as well as the struggles against war (Kosovo, Afghanistan, Iraq) and globalization. <p>
Ray was always there. He was one of the warmest souls on this planet. For Ray, to paraphrase another revolutionary, solidarity was a matter of fact--necessary to win the fight for equality and human emancipation. Ray strengthened everything we did and gave courage to everyone around him.<p>
He was at our farewell party when we left San Francisco last year--joking, laughing, talking politics, arguing his views, listening to others, reveling in our infant son and enjoying that fine, sunny afternoon. That is how we will remember him--as someone who cared deeply about humanity, worked for it, and also knew how to be an honest, consistent and unswerving friend and political ally. We will miss him terribly.<br>
<b>Eduardo Capulong and Rebecca Weston, </b>New York City<p>

