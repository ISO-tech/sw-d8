<font face="Times New Roman, Times, serif" size="4">Texas oil refinery tragedy</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Killed by corporate neglect</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 1, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>ELIZABETH SCHULTE reports on the deadly oil refinery explosion in Texas City, Texas.</b><p>

FLAMES SHOT through the air, smoke billowed in the sky, ash and charred metal rained down. The blast was so violent that it shook houses five miles away. This is how witnesses described an explosion at a British Petroleum (BP) oil refinery in Texas City, Texas, that killed 15 workers and injured 100 more on March 23.<p>
The plant, the third-largest refinery in the U.S., employs some 1,800 people in Texas City, a city of about 40,000 people 35 miles southeast of Houston along Galveston Bay. It produces 3 percent of the U.S. oil supply, processing about 433,000 barrels of crude oil a day.<p>
"Basically, it was one big boom," Wenceslado de la Cerda, a retired firefighter, told the Associated Press. "It's a shame that people have to get killed and hurt trying to make a dollar in these plants, but that's part of reality."<p>
Company spokespeople were quick to apologize. "Texas City has had a very good safety record," BP spokesperson Neil Chapman told reporters.<p>
But Wednesday's explosion is the third fatal accident in less than four years at the plant. The environmental watchdog group Public Interest Research Group reported that figures from the Occupational Safety and Health Administration (OSHA) show BP had more than 3,500 accidents or spills from 1990 to 2004. In September, two workers at the plant were burned to death and another seriously injured by superheated water.<p>
When OSHA said it was fining BP $109,500 for safety violations, including failing to maintain equipment or provide protective gear, BP immediately contested the charges. This strategy will likely pay off. When BP challenged OSHA charges over a leaking furnace pipe that started a fire and explosion in March 2004, its penalties were reduced from $63,000 to $13,000.<p>
The union that represents BP workers--the Paper, Allied-Industrial, Chemical and Energy Workers International Union (PACE)--says that it is conducting its own investigation into the accident.<p>
This is an already dangerous industry made more hazardous by the company's use of nonunion contractors to save money. "We're always concerned about safety and companies using contractors that aren't skilled and aren't familiar with the plant," Steve Lyle of PACE Local 4-1, which represents 1,000 workers at the refinery, told the Associated Press.<p>
David Michael Smith, a local labor activist with the Progressive Workers Organizing Committee, agreed. "There are eight petrochemical plants in the Texas City area, and all of them have been moving toward the outsourcing of union jobs," he told <i>Socialist Worker.</i> "If the owner could run the plants with robots they would. What they're looking increasingly for is paying workers less, slashing or providing no benefits, getting rid of any kind of long-term obligations."<p>
But in this part of Texas--an anti-union "right-to-work" state where just 5 percent of workers are represented by unions--many see this dangerous work as the only option. "There aren't very many jobs, and most jobs pay Southern wages--most jobs are not unionized," Smith said. "When people have a chance to work in the petrochemical plants, often they'll do so. When you point out the really horrendous smell in the air, it's very common for people to smile and say, 'But that's the smell of money.' Frankly no--it's the smell of benzene or some other chemical that we can't pronounce that is rotting our insides."<p>
In fact, those smells indicate another health hazard from the plants--one that affects the entire community. The <i>Houston Chronicle </i>last year conducted an investigation of the air near the area's many refineries and petrochemical complexes. The newspaper reported high levels of the carcinogen benzene in nearby Port Neches. One scientist said living there would be like "sitting in traffic 24-7."<p>
In the wake of the explosion, BP will likely try to quietly pay compensation to the explosion's victims, while doing as little as possible to correct safety violations. Yet the tragedy also provides an opportunity for labor and community organizations to collaborate on safety and environmental issues to hold the companies accountable--and fight for safe, secure jobs at a decent wage.<p>

