<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A kinder, gentler U.S. imperialism?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 1, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">THE U.S. government saw September 11 as a unique window of opportunity--a modern-day Pearl Harbor--that created ideal conditions for advancing its agenda. The agenda has been advanced under the broad rubric of the "war on terror," which has become the catchall that can justify military intervention in virtually any part of the world.<p>
But the justification must be separated from the real reasons. Afghanistan was invaded not because the U.S. was appalled by the Taliban. Indeed, the U.S. played footsy with the Taliban before it decided to destroy them.<p>
The brutality of the Taliban and its harboring of Osama bin Laden were the excuses used to insert an American presence in a part of the world that the Cold War had cut it off from in the past. As a result of the U.S. intervention in Afghanistan, women are no freer, brutal local warlords continue to operate, and opium has become the country's biggest export. In other words, all the things they used to justify the war were simply justifications.<p>
On the other hand-- the region is now dotted with U.S. military installations, and the countries surrounding Afghanistan are safely in the U.S. orbit. And <i>that</i> was the real point of the operation. <p>
Iraq was invaded not because Saddam was linked to al-Qaeda or because the U.S. feared he might give weapons of mass destruction to terrorists. These were extremely flimsy excuses to seize control of the world's second-largest oil reserves, create a friendly puppet regime and established a military beachhead in the Middle East to impose more regime changes.<p>
It is this, perhaps, that explains why bin Laden has not been captured. He's far more useful at large than he is captured. Conveniently, a new "bin Laden" has been created in Iraq, too, going by the name Zarqawi.<p>
You don't kill the bogeyman. You can arrest his "lieutenants," you can destroy one of his "hideouts." But you need him to live on. For if the war on terror is too successful, then you lose your justification for occupation, or for more interventions.<p>
Liberals can't seem to get that through their thick heads. They keep writing reports, quoting CIA documents and special reports explaining that the invasion of Iraq has only emboldened the "terrorists" and made recruiting for them easier. They think that if they repeat this argument, some U.S. official will say, "Gee, I guess you're right. Let's stop throwing our weight around so we don't provoke the terrorists."<p>
But the U.S. <i>wants</i> the bogeyman--not only abroad but at home. They want it to justify wars abroad, but also repression at home. Detention without trial--it's justified because there is an imminent "terrorist threat." (True, there is the little problem that if you throw everything you've got at "terrorism" and it doesn't go away, you aren't exactly advertising your effectiveness!)<p>
That doesn't mean that the U.S. doesn't want to crush any resistance when it arises. It really does want to destroy the Iraqi resistance to the occupation in Iraq. However, in doing so, it also wants to create the impression that all resistance to it is bin Laden-style "terrorism," and not legitimate national resistance. Therein lies the usefulness to the U.S. of Zarqawi.<p>
The problem, according to Bush's liberal critics, is that Bush isn't fighting the <i>right </i>war. He needs to go after the <i>real</i> threats. These arguments miss the whole point of the "war on terror."<p>
Obviously, the best way for the U.S. not to create any enemies in the world would be to cease being an imperial power and stop bombing people around the world. But that's like asking a tiger not to hunt.<p>
In short, liberals accept the idea that the U.S. should be the world's cop, just a nicer one. This widespread illusion must be systematically challenged within the antiwar movement for what it really is: a plea for a kinder, gentler imperialism. <p>

