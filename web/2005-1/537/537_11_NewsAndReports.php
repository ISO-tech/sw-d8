<font face="Times New Roman, Times, serif" size="4"><b>Defend abortion rights</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Dana Cloud</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AUSTIN, Texas--If the right had its way, defenders of immigrants' and women's rights would be an endangered species at the University of Texas. But their efforts are meeting resistance.<p>
On March 2, hundreds of students at the University of Texas at Austin mobilized against the scapegoating of undocumented immigrants by the Young Conservatives of Texas (YCT), who had planned a horrific "Immigrant Hunt."<p>
When the YCT appeared with their table to celebrate Texas Independence Day, they were confronted by activists from Latino and Black student organizations and the ISO. The vocal protesters surrounded the YCT's table, shouting "Racist, sexist, antigay! Born-again bigots, go away!"<p>
By the time the conservatives left (earlier than planned), their table had been dismantled, all their literature thrown in the trash, and the cookies and cake they brought for Texas Independence Day distributed to the people. Now activists have begun coalition meetings to continue antiracist organizing. <p>
The abortion-rights struggle exploded March 23-24 when the notorious national anti-abortion organization Justice For All (JFA) set up on campus with their gruesome, 20-foot-high display of images of "abortions." In contrast to pro-choice student organizations, whose leaders actually discouraged protests in favor of a counterdisplay down the street, several dozen militant protesters gathered at the display for a confrontational rally.<p>
Chanting "Pro-life, your name's a lie! You don't care if women die!" and "We're not ashamed, we're not sorry--Keep your laws off my body!" protesters marched around JFA's display. Speakers at the rally sounded the call for a new, unapologetic abortion rights movement and criticized both Republicans and Democrats for participating in the assault on our rights. One speaker pointed out the hypocrisy of bigots who defend fetuses and brain dead people while massacring innocents in Iraq and slashing welfare and other support for the living.<p>
Though the counter-demonstration to JFA was smaller than the two previous times JFA has descended on UT, its militancy was a hopeful sign that we can rebuild a real, uncompromising movement for abortion rights.<p>

