<p><font face="Times New Roman, Times, serif" size="4"><b>Defend academic freedom and dissent</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Alex Schulz and Brian Cruz</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--In a packed room of more than 300 supporters, University of Colorado ethnic studies professor Ward Churchill took on his critics.<p>
Churchill criticized the conservatives, right-wing talk show hosts, state governments and even his own university, all of whom have attacked him for writing an essay stating that the September 11 attacks killed "little Eichmans" in the World Trade Center. The comment wrongly implied that the victims were to blame for U.S. crimes overseas.<p>
Focusing on his new book, <i>On the Justice of Roosting Chickens,</i> Churchill chronicled U.S. atrocities in the search to "maximize profits" from colonial days to the war on Iraq. Churchill placed the recent attacks against him in the context of a larger crackdown on anyone who opposes the war and U.S. foreign policy in general.<p>
During the discussion, several people asked whether kicking military recruiters off schools could affect the occupation. "It's a good start, [though we need to] take it somewhere constructive," Churchill answered. While the burgeoning counter-recruitment movement alone won't stop the war, the fight is lending confidence to an antiwar movement that badly needs it.<p>

