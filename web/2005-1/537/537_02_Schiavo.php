<font face="Times New Roman, Times, serif" size="4">Anti-abortion fanatics flock to exploit Schiavo case</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Right-wing vultures</b></font> <p>
<font face="Arial, Helvetica, sans-serif" size="2">April 1, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>ELIZABETH SCHULTE reports on the hysteria whipped up by right wingers over the Schiavo case.</b><p>

RICHARD ALAN Meywes was arrested last week when he offered a $250,000 reward to anyone who killed the husband of Terri Schiavo. And that was wasn't only crime committed by the right-wing hypocrites and anti-abortion fanatics who flocked to exploit Schiavo, the severely brain-damaged woman whose feeding tube was removed March 18, after a Florida judge's ruling.<p>
It wasn't even the only crime committed by Meywes--who also offered $50,000 to have the judge killed.<p>
As <i>Socialist Worker </i>went to press, it appeared that Schiavo's parents--who took their fight to get the feeding tube reattached to the U.S. Congress and Supreme Court, and won the support of George W. Bush along the way--had exhausted their legal options.<p>
Schiavo has been kept alive by a feeding tube since she suffered a heart attack in 1990. One judge after another has accepted testimony from doctors who said that Schiavo was in "persistent vegetative" state, with no hope of recovery. Yet Michael Schiavo, who has said his wife told him she wouldn't want to be kept alive artificially, had been prevented from having the feeding tube removed and allowing his wife to die.<p>
All along the way, Terri's parents have been supported by people who think they have something to gain from this case--the religious right.<p>
Randall Terry is one of them. He's the founder of Operation Rescue, an anti-abortion group that in late 1980s and early '90s organized protesters to harass women coming in and out of abortion clinics.<p>
One of his followers was a man named James Kopp, who was convicted and sentenced to 25 years in prison for the 1998 murder of a Buffalo, N.Y., doctor who performed abortions. Today, Terry is hoping that the publicity he gets around the Schiavo case will help him re-emerge from the shadows.<p>
The "Terri Schindler-Schiavo Foundation" Web site offers links to several right-wing groups, from the rabidly anti-choice National Right to Life--whose conference Schiavo's brother Bobby spoke at last year--to radio "personality" Glenn Beck, who is selling shirts that say "I starved my wife to death and all I got was this lousy T-shirt."<p>
Then there's Tony Perkins of the Family Research Council, whose Web site advertises such publications as <i>Dark Obsession: The Tragedy and Threat of the Homosexual Lifestyle </i>and claims that "'hate crimes' against homosexuals are consistently over-reported."<p>
These are the vultures exploiting the Schiavo case.<p>
Members of Congress and the president of the United States want a piece of the action, too. House Minority Leader Tom DeLay (R-Texas) led the crusade--even though he and his family made the decision to disconnect his father from life support 16 years ago after he suffered a traumatic accident.<p>
But if the politicians thought they would make political gains from pandering to the Christian Right in the Schiavo case, they were mistaken. Opinion polls taken in the week Bush rushed back from his Texas vacation to sign emergency legislation that allowed the Schiavo case to go to the federal courts showed that the public sees through the politicians' talk about the "sanctity of life."<p>
ABC News reported that, by a 67-19 percent margin, "most think the elected officials trying to keep Schiavo alive are doing so more for political advantage than out of concern for her or for the principles involved." According to a CNN-<i>USA Today</i>-Gallup poll conducted just after Bush intervened, the president's approval rating plummeted to an all-time low of 45 percent.<p>
But this hasn't stopped Florida Republican Rep. Dave Weldon from announcing plans to put forward legislation that will make it easier for the courts to meddle in cases like Schiavo's. If the right wing and Washington's shamelessly self-promoting politicians have their way, the Schiavo case won't be over when Terri Schiavo is finally allowed to die.<p>

