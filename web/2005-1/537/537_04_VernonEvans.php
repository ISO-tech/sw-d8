<font face="Times New Roman, Times, serif" size="4">Racism is alive and well on Maryland's death row</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>They call this justice?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 1, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">VERNON EVANS' latest request for a stay of execution has been denied. This decision by a Baltimore County circuit judge was no surprise and will clear the way for a higher court to render an opinion on Vernon's request.<p>
Having been an anti-death penalty activist for more than a decade, I've learned not to confuse principle--or even basic morality--with the bizarre machinations of the courts. Still, as anticipated as the decision was, it is a slap in the face, and I am angry about it.<p>
What the judge decreed, in essence, was that Vernon's claim that he is a victim of the proven racism that riddles Maryland's death penalty process was without merit. Never mind that in 2003, a $250,000 study of Maryland's death penalty, commissioned by the governor, found that Blacks who kill whites are two times more likely to be sentenced to death than whites who kill whites, three times more likely than Blacks who kill Blacks, and almost 11 times more likely than other combinations.  <p>
Vernon is a Black man accused of killing two white victims in Baltimore County. He never had a chance. In Vernon's trial, prosecutors used eight out of 10 of their pre-emptory strikes to remove potential Black jurors. Furthermore, a study performed by Howard University criminologist Richard Selzer showed that during this period, county prosecutors were striking Black jurors at twice the expected rate. <p>
Across Maryland, every year, Blacks account for approximately 80 percent of murder victims, yet seven out of eight of those on death row are accused of killing whites. And even though Black-on-white murder accounts for less than 5 percent of killings in Maryland, such cases make up over 62 percent (five out of eight) of Maryland's current death row population. Since the state started tracking numbers in 1923, Blacks have accounted for 77 percent of all executions and 88 percent of those executed under 25 years of age.<p>
Just days ago, Baltimore County prosecutors announced that they will seek the death penalty against John Edward Kennedy, an 18-year-old boy accused of killing the dean of a private school during a botched robbery attempt. The boy is 18 years old, but as a reporter at a local newspaper observed to me, "He looks more like 16."<p>
I guess this is the prosecutors' sick answer to the recent U.S. Supreme Court's <i>Roper v. Simmons</i> decision outlawing the execution of juvenile offenders. <p>
And--you guessed it--Kennedy is Black, the victim white.<p>
To turn from the tragic to the absurd, a week prior to the indictment of the 18-year-old Kennedy, Baltimore County prosecutors showed their sense of "humor." During a marathon prison bus ride, there was a murder. The prisoners were being transported from a maximum-security prison in Hagerstown to Baltimore for court hearings. <p>
One prisoner, Kevin Johns Jr., was a deeply disturbed, possibly schizophrenic, individual convicted of strangling his uncle and also of killing a 16-year-old cellmate. During his last court hearing, Johns begged the judge for help, claiming that he was sure he would kill again without treatment. The man Johns is accused of killing during the bus ride, Phillip Parker Jr., was being transported <i>in order to testify on Johns' behalf</i>. <p>
As a result of the killing, five corrections officers were severely punished: three fired, two disciplined. It's a full-blown scandal in the Department of Corrections. But now, enter Baltimore County. Somehow, county prosecutors have ascertained that during the hours-long bus ride, the killing occurred in their jurisdiction. And someone's gotta die.<p>
That's justice, Baltimore County-style.  <br>
<b>Mike Stark, </b>Washington, D.C.<p>
