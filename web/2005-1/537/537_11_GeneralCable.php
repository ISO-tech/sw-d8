<font face="Times New Roman, Times, serif" size="5"><b>R.I. workers strike to defend benefits</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Brian Chidester</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 1, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">LINCOLN, R.I.--Almost 200 workers went on strike March 21 at the General Cable Corporation. The workers, members of United Steel Workers of America Local 4543, are striking against the company's demand that they accept a health care co-pay.<p>
General Cable Corp. is a multinational corporation with 16 plants in the U.S., as well as operations in Spain, Portugal, Norway and New Zealand. The company is a major supplier of wire and cable to Home Depot and Lowe's and reported 2004 gross profits of $214 million.<p>
When it came time to negotiate the contract, however, General Cable offered paltry raises--$1,000 bonuses in the first two years, with raises of 2.5 percent, 2.7 percent and 3 percent in the last three years of the contract. <p>
The company also proposed that workers accept a co-pay on their health insurance in the third year of the contract, with family plans costing $22 per week. According to Local 4543 treasurer Carlos Lomba Jr., this would more than wipe out the proposed raise. "Five years from now, we'll be making less than we're making now," said striker Jerry Leite.<p>
Workers rejected the proposed contract and voted by a narrow margin to strike on March 20. Two days later, with the company offering to "split the cost" of premium increases over 14 percent--which was the union's original proposal--the union voted 127-52 to reject yet again. <p>
The main reason more people voted to reject a better offer is that many Portuguese-speaking workers who had voted to accept the first offer had never seen a Portuguese translation, so when the second offer was translated for them, many more voted to go on strike.<p>
The mood on the picket line was defiant and upbeat, with workers keeping the picket line going 24 hours a day and improvising chants such as "No way co-pay Jos&eacute;!"<p>
While the company has said publicly that it has plenty of stock to weather the strike, as many as 40 scabs were at the plant on third day of the strike, and the plant was still getting deliveries. By the fifth day, it was clear that the company was preparing a full-blown scabbing operation, with security guards from MADICorp., a professional strike-breaking company, filming the picket line. This company also offers the services of skilled workers--who could potentially keep production running during the strike, just as they did during the AK Steel strike. <p>
In order to be successful, the workers will eventually have to confront the scabs directly and stop them from entering the plant.<p>
General Cable's Lincoln plant is the only one where workers have no co-pay on their health insurance. That's why the company is taking such a hard line. In addition, not all of the company's U.S. plants are unionized, and those that are have different unions and different contracts.<p>
Forging links with these other unions and groups of workers--and using them for joint action in solidarity--will be key to the success of the strike.<p>


