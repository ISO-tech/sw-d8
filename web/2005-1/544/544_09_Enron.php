<font face="Times New Roman, Times, serif" size="5"><b>The fall of the house of Enron</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Cindy Beringer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 20, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><i><b>Enron: The Smartest Guys in the Room, </b></i>a documentary by Alex Gibney.</font><p>

<font face="Times New Roman, Times, serif" size="3">WITH GLITZ and glamour and a great sound track, <i>Enron: The Smartest Guys in the Room </i>chronicles the rise and fall of the Houston corporation that was as big and bizarre as the state of Texas itself.<p>
Based on the book of the same title by <i>Fortune</i> magazine writers Bethany McLean and Peter Elkind, the film entertains and enrages while it makes the complicated skullduggery practiced by Enron's hyper-capitalists accessible to everyone. Enron is described as a "house of cards built over a pool of gasoline."<p>
Frequently playing on the company's motto "Ask Why," the film reveals the story's many villains through interviews, news clips, training films and company ads.<p>
There are no heroes. The writers feel sure that everyone in the Enron inner circle, especially former Enron Chairman Ken Lay, knew what was going on.<p>
Lay, however, comes off in the film as some sort of hapless overseer--an old man who depended upon outlandish ideas of young go-getters to keep profits and stock prices rising. Meanwhile, he encouraged his traders to gamble while he threw money around to promote the company name and to purchase politicians who would help in his "crusade to free business from government."<p>
The presidencies of two Bushes helped make his dream of energy deregulation a reality. George W. helped promote Lay as deregulation's ambassador-at-large.<p>
Jeff Skilling seems larger than life, a cult leader who could deliver energy like stocks and bonds. His mark-to-market accounting scheme, carefully explained in the movie, blew the Enron stock bubble to the point of bursting. Approved by Arthur Anderson, mark-to-market accounting allowed Enron to use predicted future profits many years down the road to enhance its balance sheet and secure loans for new schemes.<p>
Chief Financial Officer Andrew Fastow was the "sorcerer's apprentice," inventing phony companies with names like Jedi and Rapture that propped up Enron stock. Most of these companies existed only to buy from Enron and provide a vehicle through which the future profits of mark-to-market accounting could exist.<p>
The vicious manipulation of the California energy market is also explained for the layman. The film airs the audio tapes of California energy traders during the California wildfires, which shut down a major transmission line into California, cutting power supplies and raising prices. "Burn, baby, burn" said one celebrating trader. In the tapes, the traders also describe their perverse plans for "Grandma Millie" and her energy bill.<p>
The list of the guilty is endless. Arthur Anderson shredded tons of documents. Merrill Lynch bought three Nigerian barges from Enron in a sham deal to boost Enron's balance sheet.<p>
The revelations of the eccentricities of the tragedy's many sociopaths prevent any dull moments. Lou Pai, the reclusive book-cooker and stock-dumper with a yen for strippers, got out while the getting was good. He's now the second largest landholder in Colorado. <p>
There's the cult of Skilling, the result of the guru's makeover from chubby nerd to lean, mean organizer of death-defying motorcycle road trips. When Skilling got Lasiks eye surgery, everyone at Enron ditched their glasses, too.<p>
It's fun to watch the egos of these maniacs being smashed, at least temporarily, and fun to watch them sweat under questioning. However, there's enough gold in their parachutes to cushion any landing for some time to come.<p>
As might be expected of <i>Fortune </i>analysts<i>, </i>the writers see the Enron disaster as classical human tragedy of "so much potential" brought down by hubris. The real tragedy, of course, occurred for the people of California, the teachers of the Texas teacher retirement fund, and the lower-ranking employees of Enron--to mention just a few.<p>
An interviewee near the end admits that little has changed as a result of the Enron fiasco. The collapse of Enron is a classical tragedy of capitalism, a system that allows, even demands, such excesses and human suffering.<p>

