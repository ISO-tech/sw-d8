<font face="Times New Roman, Times, serif" size="5"><b>Driving out the bigots</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Yasser Giron</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 20, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">SOME 500 counterprotesters drove out the racist anti-immigration groups Save Our State and the Minutemen, shutting down a May 14 demonstration in Baldwin Park, Calif., a dozen miles east of Los Angeles.<p>
Save Our State claims that California is being turned into a "Third World cesspool" and that state's economic problems are the result of "illegal aliens." Members of the hate group are also part of the Minutemen Project, which descended on the U.S.-Mexico border in Arizona to terrorize immigrants.<p>
On May 14, Save Our State called for a rally at Baldwin Park Metro rail station to protest a monument created to honor the area's Mexican and Native American history and culture. <p>
But a counterdemonstration organized by the ISO, Southern California Human Rights Network, MEXA/MEChA, Frente Unido de las Americas and other community and student groups vastly outnumbered the 20 racists who gathered a block away from the site. The anti-immigrant demonstrators needed protection from a heavy police presence.<p>
Baldwin Park Mayor Manuel Lozano told the counterprotesters, "This monument belongs to the community and will be here 50 years from now." Judy Baca, the artist who created the monument, said, "I stand alongside the memories of those who fought against colonialism."<p>
Over a period of three hours, counterdemonstrators approached the police boundaries to get as close as possible. Several times, the cops threatened to move in and arrest us, but we held our position and continued chanting down the racists. After some time, Save Our State tucked tail and left under police escort. <p>
Two factors prevented the counterdemonstration from being even bigger. First, after about an hour, the demonstration split in half, with some people retreating from confronting the racists to rally at a previous staging area. Others were convinced by the argument that Save Our State is a distraction--and decided not to participate in the counterprotest at all. <p>
But the tactic of confronting the racists proved successful. As the <i>Los Angeles Times </i>reported, Save Our State member Randy Selenak said that going to the Latino suburb of Los Angeles was "like going into a lion's den. I just want to get out of here in one piece." <p>
Groups like Save Our State and the Minutemen thrive because they get cover from politicians like California Gov. Arnold Schwarzenegger, who recently praised the Minutemen vigilantes. This makes their views seem legitimate. Unity, solidarity and direct mass confrontation can show hate groups like Save Our State that their bigotry will not be tolerated--and that they will be met with resistance at every turn. <p>
The groups who turned out in Baldwin Park are organizing a contingent to confront these racists when they come to San Diego in August. Join us in confronting the right wherever they raise their heads.<p>

