<font face="Times New Roman, Times, serif" size="5"><b>The left's fight for disability rights</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 20, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">RAVI MALHOTRA'S claim that the left ignores disability rights, like the related argument that disability is "beyond left and right," is simply wrong ("Left ignores disability rights," May 6). <p>
The society we live in is particularly callous to people with disabilities, offering practically nothing in the way of access or support, and in countless ways marginalizing them and discriminating against them.<p>
While it is true that the U.S. left is currently weak in many areas, the left has always defended the things that matter most to people who are disabled--such as Medicare, Medicaid and anti-discrimination protection in the workplace, while fighting for the things that would radically improve quality of life for the disabled, such as universal health care, child care, public transportation and workplace accommodations.<p>
The right-wing neoliberal agenda, however, is about destroying what's left of state programs and weakening the rights of the poor and workers--which means taking away vital life-lines especially for people with disabilities. <p>
The current attack on Social Security is a classic example of a right-wing policy that jeopardizes the meager support that is currently relied on by countless senior citizens with disabilities. And don't forget that imperialist war, opposed pretty consistently by the left, daily inflicts wounds that create legions of newly disabled people--Iraqis and Americans--while utterly failing to help them to rebuild their lives.<p>
In my experience, even though radical groups lack the resources available to the wealthier, right-wing establishment, our meetings are far more likely to be held in buildings that are wheelchair accessible than in most other areas of life; I know that for the ISO, it has always been a priority to make our meetings and organization accessible.<p>
It is also wrong to counterpose the right to control our lives against the right to control how and when we die. Those of us who do live with a disability, as much as those who don't, should be able to make that decision for ourselves, and if I am ever unable to speak for myself, I know I would trust my next of kin above George Bush and the Religious Right to make that call for me.<p>
It's for precisely such reasons that Helen Keller became a revolutionary socialist.<br>
<b>Helen Scott, </b>Burlington, Vt.

