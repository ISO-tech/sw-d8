<p><a name="No"></a><font face="Times New Roman, Times, serif" size="4"><b>No death penalty</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jessica Hansen-Weaver</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--More than 50 came to a meeting of Campaign to End the Death Penalty (CEDP) in Bayview on April 30. <p>
Speakers included Barbara Becnel, advocate of innocent San Quentin death row prisoner Stan Tookie Williams; Charlene Smythe from the Green Party; Mesha Monge-Irizarry of the Idriss Stelley Foundation; and Sandra-Juanita Cooper (CEDP). The event was organized to connect the dots between the violence that is happening in the community with the oppression that is happening on San Quentin's death row and in society at large. <p>
The evening was highlighted by the stories of Doris McClean, mother of a death row inmate who has been denied any communication with her son, who was recently shot in the face by a prison guard, and the family of Julio Ayala, who was killed by five police officers in early April.<p>

