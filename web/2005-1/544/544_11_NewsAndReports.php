<p><a name="Recruiters"></a><font face="Times New Roman, Times, serif" size="4"><b>Recruiters out of our schools</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jesse Hagopian</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--More than 60 parents, students, teachers and community members packed the Garfield High School Library for the May 12 Parent Teacher and Student Association (PTSA) meeting to voice their opposition to the presence of military recruiters on school grounds. The overwhelming majority of parents passed an anti-recruitment measure.<p>
"There is no place for liars to come to our campus and recruit our kids to die for an illegal war," argued one PTSA member, citing a <i>New York Times </i>article in which recruiters admitted bending the rules to lure in recruits.<p>
The PTSA resolution states, in part, "There are many reasons why Garfield parents, teachers and students might object to military recruiting on campus. Not all of us agree on every issue. Whether it is because of a desire to protect young students from the life-and-death decision that military service presents, objection to the current war in Iraq, fear that recruiters may not present a realistic picture of military life or disagreement with policies that discriminate on the basis of sexual orientation, we do agree that public schools are not a place for military recruiters." <p>
While the PTSA's resolution does not legally bind the school to remove the military, it has already energized the anti-recruitment movement in Seattle and could be a significant step in a larger campaign to pressure the citywide school board to eject military recruiters from all Seattle schools.<p>

