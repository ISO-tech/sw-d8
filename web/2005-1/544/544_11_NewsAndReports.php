<font face="Times New Roman, Times, serif" size="4"><b>Save Seattle schools</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Vicky Jambor, Seattle Education Association</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--Two hundred parents and students filled the Seattle Public School Board meeting May 4 to express their anger about the proposal to close 10 schools and consolidate at least 14 other sites and programs.<p>
Superintendent Raj Manhas announced the cuts April 21, claiming they were necessary to close a $20 million deficit. The cuts would also eliminate the busing program and re-segregate schools by forcing children to attend neighborhood schools.<p>
Parents and students rallied before entering the school board meeting. "Seattle Schools sold our city," they chanted. "Save our schools." Inside, it was standing room only as parents and students spoke out to their elected school board members about the cuts. "I will not allow our school superintendent, our board of directors--anybody--to wager my son's future in their game of craps," said parent Al Boss. "Our children are not crap, and they deserve better."<p>
Since the announcement of the proposed closures, parents, teachers and community members have organized actions throughout the city ahead of the July 13 school board vote on the plan. One important outcome has been the emergence of a citywide coalition, Communities for Public Education, which is fighting to stop the proposed plan to close schools.<p>
About 100 parents and students protested May 14, at a celebration of the completion of the new City Hall, challenging Mayor Greg Nickels with a protest. One of the local schools on the list to be closed is T.T. Minor, which had its marching band play in the ceremony. All of the students wore "Save Our School" buttons, and parents started chanting "Save our school!" when they were done playing.<p>
Another rally was set for the school board meeting May 18, followed by a citywide rally in early June.<p>

