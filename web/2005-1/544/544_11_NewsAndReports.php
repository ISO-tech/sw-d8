<p><a name="BPP"></a><font face="Times New Roman, Times, serif" size="4"><b>Black Panther Party reunion</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--An inspiring tribute to the Black Panther Party's Seattle chapter was held May 14 when a dozen former members of the local Black Panther Party and those from Oakland, Calif., held a reunion. More than 400 people attended speeches, teach-ins, a film festival and musical performances in the first such reunion held outside California. <p>
Aaron Dixon, a former Black Panther Party captain, announced to the crowd, "This is not just a reunion. We have come to set the record straight." Bobby Seale, co-founder of the Black Panther Party in Oakland with Huey P. Newton, called the Seattle chapter "probably the most dynamic and most profound" because of its humanitarian work. <p>
"What's interesting is, in 1968 and 1969, we created these survival programs because all the money was being drained for the war in Vietnam, which is the same thing now," Dixon said.  "All the money that should be going to our communities is going to the war in Iraq. It's the same scenario."<p>
 

