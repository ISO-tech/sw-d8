<font face="Times New Roman, Times, serif" size="4">Union opposes cuts in campaign for a new contract</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>SF teachers step up the fight</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Andrew Libson, United Educators of San Francisco</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 20, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--About 350 teachers and paraprofessionals from United Educators of San Francisco (UESF) lined the streets of San Francisco during rush hour May 11 to build support for public education in San Francisco. The event was part of a "Day of the Teacher" demonstration in which educators across the Bay Area came out to show their displeasure with statewide attacks on public education.<p>
UESF members have been working under an expired contract for the last year, and the San Francisco United School District is looking to extract major concessions in the latest rounds of negotiations. The district has already chipped away at retiree health care and now is looking to freeze teacher pay, restrict benefits, and reduce the authority of the union in the schools.<p>
The district claims there isn't enough money for schools this year, but somehow it found a way to give District Superintendent Arlene Ackerman a 12 percent salary increase along with a slew of other perks.<p>
This was the first major demonstration called by UESF to build support for upcoming contract fight, and was a good start for a union whose last major action was more than 20 years ago. The key will be building awareness and support for the contract fight at each school. This needs to be connected to promoting more rank-and-file involvement in a union that has atrophied since its last major contract fight in 1979.<p>
The "Day of the Teacher" protests had a second target, California Gov. Arnold Schwarzenegger, who declared war on the California teachers' unions, calling them "special interests." The governor has called for "merit pay" for already poorly paid teachers and a lengthening of the "probationary period" for teachers from two to five years. This will only increase job insecurity among educators, who have grown accustomed to yearly rounds of layoffs and program cuts.<p>
Lastly, Schwarzenegger is reneging on $2 billion promised for public education as a means of balancing the state budget. Educators across the Bay Area highlighted these attacks from Schwarzenegger with signs like "Children are our Special Interest" and "Who do you believe? The Educator or the Terminator?"<p>
The statewide California Teachers Association is turning up the heat on Schwarzenegger, whose approval ratings have already plunged from 67 percent to 44 percent over the last several months. A larger demonstration is planned for May 25 in Sacramento that will bring together different unions to protest Schwarzenegger's arrogant, anti-worker policies. <p>

