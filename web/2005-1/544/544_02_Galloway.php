<font face="Times New Roman, Times, serif" size="5"><b>Oil-for-food witch-hunt </b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 20, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">REPUBLICANS HAVE more targets in their witch-hunt of the United Nations' (UN) oil-for-food program--European political figures who dared to defy Washington's brutal economic war on Iraq during the 1990s. But a well-known antiwar activist from Britain, George Galloway--newly elected to a seat in parliament earlier this month--is set to testify in Washington to refute the allegations.<p>
Members of both houses of Congress have been investigating alleged corruption in the UN program--which allowed Iraq, facing the strictest regime of international sanctions ever known, to sell some of its banned oil on the world market, and use the proceeds to buy food, medicine and other necessities.<p>
Last week, the Senate Government Affairs Permanent Subcommittee on Investigations released a report claiming that Saddam Hussein and his government rewarded nearly 300 international figures, including French and British politicians, for opposing UN sanctions by granting them allocations of oil to sell.<p>
Former French Interior Minister Charles Pasqua--an ally of French President Jacques Chirac--is one of the accused. Pasqua says that the charge has been made before and thoroughly discredited.<p>
Another target is Galloway, a former member of Prime Minister Tony Blair's Labour Party, who was expelled from Labour in 2003 amid similar allegations that he was paid off by the former Baathist regime. Galloway sued for libel against two newspapers--Britain's <i>Daily Telegraph </i>and the <i>Christian Science Monitor</i>--after they printed the charges, and won both cases.<p>
In this month's British elections, Galloway embarrassed Blair by winning back a seat in parliament as a candidate of the antiwar Respect Coalition. Galloway challenged the committee to hear his testimony, and shamed them into granting him time to speak this week.<p>
The charges against Galloway revolve around a campaign he launched against the UN sanctions--the Mariam Appeal, named after a four-year-old Iraqi girl named Mariam Hamza, who suffered from leukemia, untreated because of the lack of medical supplies. The Senate committee claims to have Iraqi Oil Ministry contracts that implicate the appeal, Galloway and a Jordanian businessman, Fawaz Zureikat, who supported Galloway's campaign.<p>
Zureikat acknowledges that he sold Iraqi oil to international companies--as a small part of his other business interests in Iraq--but says that his sales were above-board, and scrutinized by UN officials.<p>
Galloway says that he never had any connection to Iraqi oil sales--and points out that no politician or newspaper has ever presented any evidence that he profited from them. "Believe me, if Saddam Hussein or anybody else had ever given me the huge sums of money that these contracts generated, you would already have the check in your hand," Galloway told Britain's <i>Independent </i>newspaper. "You would already have my bank account details. The United States security services would have provided you with all of that."<p>
Galloway denounced the Senate committee--chaired jointly by right-wing Republican Sen. Norm Coleman (R-Minn.) and Democrat Sen. Carl Levin (D-Mich.)--for issuing a report before ever interviewing him, calling them "lickspittles" for the Bush administration who defend the U.S. war on Iraq.<p>

