<font face="Arial, Helvetica, sans-serif" size="2"><b>OBITUARY: OSSIE DAVIS</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Give them a little truth, a little hope"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Mike Stark</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 11, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">IN 1999, I was driving Ossie Davis around Washington, D.C., during a visit he made to participate in a delegation to the White House in support of Pennsylvania death row prisoner Mumia Abu-Jamal. We talked about the inspirational and often surprising ability of ordinary people at moments in history to stand up and change the world for the better.<p>
Drawing on his Georgia roots, Ossie said, "Many people look at a horse who eats sawdust, and say, 'What a stupid, lazy horse. That horse will never be anything.' But if you feed that horse some oats, you'll see how quickly that horse will learn to jump. Organizing is the same way. People are used to lies and garbage. But if you give them a little truth, a little hope, you'll be amazed at how they react."<p>
The movement for social justice lost a great champion last week when Ossie Davis died. Davis' distinguished career as an actor and his life as an activist spanned several eras--from the turbulent 1930s to the anti-communist witch-hunts of the 1950s, to the civil rights movement of the 1960s, continuing until today, with the current struggles against the death penalty, against racism, against war.<p>
Davis' life was characterized by struggle--both his own struggle against racism as an actor, and the movements for social justice and equality that he threw himself into. He was a luminary in both areas--on a par with his friend and mentor Paul Robeson.<p>
Raised in Georgia, Davis came of age in the 1930s, an era in which the struggle against racism inside the U.S. was taking a new turn. Davis said that as a young man, he dreamed of joining Ethiopia's struggle against the invasion of Italy's fascist Mussolini dictatorship--although he confessed he wasn't sure where Ethiopia was.<p>
Davis later attended Howard University, where he came in contact with the struggle to save the Scottsboro Boys from execution in Alabama. He was also inspired by hearing the African American singer Marion Anderson sing at the Lincoln Memorial in 1939. Anderson was prevented by segregation from performing in venues such as New York's Metropolitan Opera until much later.<p>
Davis later moved to Harlem to pursue a career in acting. There, he came in contact with other radicals, including members of the Young Communist League. His career as an actor began with the Rose McClendon Players, a theater group in Harlem, which was the center of Black culture in the U.S. He was deeply influenced by other prominent radical artists, such as Langston Hughes, Richard Wright, Paul Robeson and others. <p>
Davis remained fiercely loyal to Robeson, even after the singer was denounced by other Black political, sports and show business figures for his openly communist and pro-USSR sympathies.<p>
Davis was at the heart of the struggle when the Black movement emerged in the 1960s. He delivered a stirring eulogy when Malcolm X was assassinated in 1965, and another for Martin Luther King in 1968. Davis and his wife Ruby Dee also helped organize the 1963 March on Washington and together were the masters of ceremonies.<p>
Davis' commitment to the struggle never wavered, and he was tirelessly active, even as he grew older. Activists in the struggle against the death penalty, in particular, have been able to count on Davis' support--he was, for example, an important part of the struggle for freedom for Lawrence Hayes, a former Black Panther who was wrongfully imprisoned a number of times.<p>
In his eulogy for Malcolm X, Davis said in his signature baritone voice that we had lost "our own Black shining prince." In Davis, we have lost someone who was also a shining light--but his example will continue to guide our movement for a better world.<p>

