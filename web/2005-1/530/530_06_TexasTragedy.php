<font face="Times New Roman, Times, serif" size="4">Roots of a tragedy in Texas</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"The war changed who my son is"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Cindy Beringer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 11, 2005 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">JUST BEFORE the Iraq war began, Rev. Johnny Williams Sr. and his wife Patricia went on an East Texas television station to talk about their faith that God would protect their Marine son in that war, and send him home "better and whole." Now his mother says, "Something happened to my son. Some of the things that he endured I may never know. But it changed who he is, and I'm sorry."<p>
Johnny Lee Williams was arrested January 22 in Arizona and charged with the murder of Megan Holden of Tyler, Texas. The abduction of the 19-year-old woman from a Wal-Mart parking lot as she left her job around midnight on January 19 was recorded on a surveillance tape and received national media attention. Holden's body was found two days. <p>
According to police, Williams then robbed a convenience store in Odessa, Texas, and was shot in an attempt to rob an RV park store in Arizona. He was arrested when he went--in Holden's truck--to a hospital to have his wound treated.<p>
Williams' parents said on ABC's <i>Good Morning, America </i>that their son came home from Iraq a "different person." He was quieter, slept in his clothes, and often asked friends to "come over and just stay while he was going to sleep," they said.<p>
Rev. Williams said that his son was especially troubled by having killed three people, including a child, while stationed in Iraq. Williams' mother told television station KLTV of her son's flashbacks about things he had to do in Iraq and his deepening depression. "Mom, some of the stuff they make you do is all messed up," her son told her.<p>
Like many other soldiers with symptoms of post-traumatic stress disorder, Williams sought, and didn't receive, help from the military and Veterans Administration (VA) hospitals.<p>
His aunt told a Tyler newspaper that Williams had been quieter and angrier since he returned from Iraq. "I guess he just snapped," the aunt said. "He's been asking for help ever since he came home."<p>
Williams' former high school classmates echo his mother's description of her son's happy-go-lucky personality before he went to war. They were shocked that Williams had been arrested for murder. <p>
Patricia Williams wants to know what happened during the war to change her son and why the military and VA refused to help him with his depression. "Somebody had to see the difference," she said. "Why wasn't something done?" <p>
Williams had received several awards for exemplary service during his three-and-a-half years in the Marines. The military takes every opportunity to parade such "trophy soldiers" before the cameras--but has no support for them when they show signs of post-traumatic stress. Williams was given a bad-conduct discharge for using marijuana a year ago, and has been on appellate leave awaiting a review of his case ever since--without pay and allowances, and without any help for his depression.<p>
Now, he is behind bars awaiting an extradition hearing to the east Texas town of Tyler--which is known for its azalea trail, its racism and its stagnant economy. <p>
Speculation that Williams, who is African American, would face the death penalty began as soon as he was arrested. If the media coverage so far is any indication, there will be a lot of talk about Williams' drug use and traffic citations--and little about his military service.<p>
"This crime was very barbaric and torturous," said Smith County District Attorney Matt Bingham added, "and we fully intend to get justice for Megan Holden." But what about justice for Williams and other soldiers like him who crack after being forced to view and commit crimes of barbarism and torture on a much wider scale in Iraq?<p>
Johnny Lee Williams' mother got it right. "They trained him to go to war and to kill," she said. "I don't know how many months went into that training, but how many months went into getting him back to being in society?" <p>
The answer is none. When their "heroes" are no longer of any use to the military, they are sent back home to their communities--to deal with their demons on their own. <p>
No one can know for sure if Holden would be alive today if Johnny Lee Williams had not been forced to kill in a war based on lies and empire. But it would be worth it for the town to consider. Williams deserves a good lawyer and a fair trial. And if justice is to be truly served, the entire senseless war on Iraq and its perpetrators will be on the stand with him.<p>

