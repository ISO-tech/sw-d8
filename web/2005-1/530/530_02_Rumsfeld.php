<font face="Times New Roman, Times, serif" size="5"><b>Rumsfeld's drive for bunker-buster nukes </b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 11, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">DEFENSE SECRETARY Donald Rumsfeld is pushing to revive a federal program to develop so-called "bunker-buster" nuclear weapons, according to a press report.<p>
Last month, Rumsfeld sent a memo to then-Energy Secretary Spencer Abraham saying that next year's budget should include funds for a renewal of the Robust Nuclear Earth Penetrator (RNEP) program. The program is supposed to create "bunker buster" bombs that can strike targets deep underground--by retrofitting low-level nuclear bombs with harder casings. <p>
The Pentagon says it needs "bunker-buster" nukes to fight terrorists with hidden caches of "weapons of mass destruction." "We can't necessarily match Cold War weapons to the new threats," Pentagon spokesman Major Paul Swiergosz told Agence France Presse. "We have to adapt capabilities that we have to meet the threats." <p>
The Pentagon doesn't seem to mind what most scientific experts agree would be the bigger threat if a bunker-buster nuke were ever used--the tons of radioactive debris sent into the atmosphere.<p>
Meanwhile, Rumsfeld himself had other matters on his mind than the leak about the bunker-buster program--like avoiding being tried for war crimes. Rumsfeld conceded to reporters that a lawsuit filed by the Center for Constitutional Rights with German prosecutors--which indicts him and other Bush administration officials for their role in the Abu Ghraib prison torture scandal--might keep him from attending a conference on security policy in Munich.<p>
Since the U.S. is not a member of the International Criminal Court, Rumsfeld cannot be tried in America. German law, however, allows charges to be made in war crimes and human rights cases regardless of the nationality of the accused.<p>
Since 2002, the Bush administration has spent more than $10 million to study the possibility of converting conventional warheads into bunker busters. In the run-up to the election last year, however, the Pentagon was forced to quietly remove $27.5 million earmarked for the project--after a five-year projection by the National Nuclear Security Administration estimated that spending on the program could end up totaling nearly $500 million for 2005-2009 alone.<p>
Now, the Bush administration wants to revive the program--even as it accuses countries like Iran of developing nuclear weapons. As International Atomic Energy Agency director Mohamed El-Baradei told <i>Newsweek</i>. "You can't tell everyone 'don't touch nuclear weapons' while continuing to build them." <p>

