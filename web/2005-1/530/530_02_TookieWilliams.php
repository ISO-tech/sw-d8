<font face="Times New Roman, Times, serif" size="4">Appeal for "Tookie" Williams rejected</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Sent closer to the death chamber</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 11, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">A FEDERAL appeals court has pushed Stan "Tookie" Williams closer to California's execution chamber, refusing last week to hear his appeal for a new trial.<p>
Williams--the former leader of Los Angeles' Crips gang who has since worked to end gang violence from behind the walls of San Quentin--is probably the most famous prisoner on California's death row. The author of three books aimed at teaching children about gang life, Williams has been nominated five times for the Nobel Peace Prize and four times for the Nobel Prize for literature.<p>
From his prison cell, Williams has urged rival gangs to negotiate peace agreements and set up a Web site, "Tookie's Corner" (www.tookie.com), to spread this message. Last year, actor Jamie Foxx portrayed Williams in <i>Redemption,</i> a cable TV movie about his life.<p>
Williams' lawyers requested a new trial on the grounds that African Americans were barred from serving on the all-white jury that sentenced Williams to death in 1981 for four murders committed in two separate robberies in 1979. The prosecutor in Williams' case had already been reprimanded several times for using the same racist practice in other death penalty cases.<p>
Nine of the 24 justices on the 9th U.S. Circuit Court of Appeals--four short of what was needed to uphold the appeal--agreed that racial bias had indeed marred Williams' trial. "I dissent from the denial of rehearing...not only because every defendant is entitled to a jury that is unbiased and untainted by racial discrimination in the jury selection process, but also because the very legitimacy of our system of justice depends upon continued vigilance against such practices," Judge Johnnie Rawlinson wrote in a dissent.<p>
Williams' next step is an appeal to the U.S. Supreme Court. If the high court doesn't take up his case, Williams could face an execution date later this year. Then, it will be up to California Gov. Arnold Schwarzenegger to decide on clemency.<p>
On January 19, a mentally disabled man, Donald Beardslee, became the first person to be executed in California in three years. It's time for anti-death penalty activists will have to turn up the heat--as they did last year in stopping the planned execution of Kevin Cooper.<p>

