<font face="Times New Roman, Times, serif" size="4">Same-sex marriage ban ruled unconstitutional</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The fight for gay marriage breaks out in New York</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 11, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">A DECISION by a New York judge has opened up a new front in the struggle for marriage equality.<p>
State Supreme Court Justice Doris Ling-Cohan ruled that New York's Domestic Relations Law--which prohibits gay and lesbian couples from marrying--violates the state's constitution by denying the right to equal protection and due process. The ruling came in a lawsuit filed by gay couples who were denied marriage licenses by the city clerk in New York City.<p>
"Similar to opposite-sex couples, same-sex couples are entitled to the same fundamental right to follow their hearts and publicly commit to a lifetime partnership with the person of their choosing," Ling-Cohan wrote. Therefore, she said, words like "husband and wife" and "groom and bride" that are currently used to define legal marriages under state law "shall be construed to apply equally to either men or women."<p>
Ling-Cohan compared the law--which dates back to 1909--to the bans on interracial marriage in the segregation-era South.<p>
That's the right comparison, according to Curtis Woolbright, who with his partner was one of the couples to file the suit. Woolbright told the <i>New York Times</i> that he joined in the court challenge in part because his own parents, a biracial couple, had been barred from marrying in Texas in the 1960s--and had to move to California to get married. Now, Woolbright told the <i>New York Times</i>, he and his partner are "so excited about this we can't express it."<p>
The ruling puts billionaire New York City Mayor Michael Bloomberg on the hot seat. Bloomberg claims that he personally supports same-sex marriage. But after the decision, Bloomberg said that he was obligated to carry out state laws--and announced that he would appeal the ruling.<p>
So far, most local Democrats--in particular, Bloomberg's rivals in the coming mayoral election--have been critical of the decision to appeal.<p>
But the Democrats can't be trusted to uphold marriage equality. During the campaign last year, party leaders, including presidential candidate John Kerry, opposed gay marriage, condemning local officials--like San Francisco's Democratic Mayor Gavin Newsom and Jason West, the Green Party mayor of New Paltz, N.Y.--who dared to issue marriage licenses to same-sex couples.<p>
In fact, just days before Ling-Cohan's decision, another New York judge reinstated misdemeanor charges against West for performing more than two dozen same-sex marriages last year. If convicted, West could go to jail for up to a year.<p>
Activists are mobilizing to defend the New Paltz mayor--and build the struggle for equal marriage. On February 7, some 400 people turned out on 24 hours' notice for a meeting called by Empire State Pride Agenda, Lambda Legal Defense, Marriage Equality and the LGBT Community Center to celebrate the courtroom victory and urge the city to begin issuing licenses.<p>
Ling-Cohan delayed the effect of her ruling pending an appeal, and her decision covers only New York City, not the entire state.<p>
Nevertheless, this ruling is a cause for celebration that can open the door to future legal challenges--and even more importantly, renewed activism in the fight for equal marriage. We need to send the message that we're through with back-of-the-bus treatment--and we want equal marriage now.<p>

