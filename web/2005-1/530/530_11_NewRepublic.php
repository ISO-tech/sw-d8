<font face="Times New Roman, Times, serif" size="4">February 11 &#8226; 5 p.m.</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Demonstrate against <i>The New Republic</i></b></font><p>

<blockquote>
<font face="Arial, Helvetica, sans-serif" size="3"><b><i>Read <a href="http://www.counterpunch.com/cockburn02052005.html" target="_blank">Alexander Cockburn's column</a> on this protest.</i></b></font>
</blockquote>

<font face="Times New Roman, Times, serif" size="3">The <i>New Republic </i>magazine has decided to publish an article that fantasizes about torturing and murdering antiwar activists. <p>
In his article, which appeared in the magazine's online edition and now appears in the upcoming print edition, "writer-researcher" T.A. Frank, fantasizes about the killing and torture of Arundhati Roy and Stan Goff, two of the international antiwar movement's best-known figures. He indulges in this fantasy while sitting in on a January 20th counter-inaugural/antiwar panel discussion featuring Goff, exonerated death row prisoner Shujaa Graham and <i>International Socialist Review </i>coeditor Sherry Wolf.<p>
Frank's violent rant cannot be defended as a work of satire. Nor is there an acceptable rationale for the <i>New Republic's </i>decision to publish such a piece, which closes with the author's bloodthirsty wish to "take a bunker-buster to Arundhati Roy." <p>
At a political moment when the Bush administration and the corporate media are trying their best to vilify all dissent, Frank's outrageous tract represents a crude but insidious attempt to intimidate the antiwar movement. Such a provocation should be answered clearly and firmly: Our voices will not be silenced.<p>
We are calling for a demonstration Friday, February 11 at 5 p.m. at the <i>New Republic's </i>D.C. Offices at 1331 H St. There, we will demand a formal apology from the editors. Call 202-667-0049 for more information. <p>
You can also write the <i>New Republic </i>at <a href="mailto:letters@tnr.com">letters@tnr.com</a> and/or contact them by phone at 202-508-4444--to let the magazine know what you think. <p>

