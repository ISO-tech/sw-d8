<font face="Times New Roman, Times, serif" size="4">A call for solidarity with Egyptian socialists</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Jailed for speaking out</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 11, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">ON JANUARY 28, Egyptian police detained three pro-democracy activists at the Cairo International Book Fair. <p>
Bahu Bakhsh, Marwa Faruk and Ibrahim al-Sahari were arrested for handing out leaflets calling for a February 4 rally to oppose President Hosni Mubarak's reelection or the prospect of his handing over the presidency to his son Gamal. The three--members of the Socialist Studies Center, an independent organization in Cairo that publishes books and holds seminars on socialism--are accused of possession, publication and distribution of leaflets that "undermine general security."<p>
Ibrahim and Marwa were arrested for antiwar activities in April 2003, and Ibrahim was severely beaten during his detention. Ibrahim has already told human rights workers over the phone that he was assaulted by police during his arrest on January 28. Bahu and Marwa have since been released, but Ibrahim continued to be detained as <i>Socialist Worker</i> went to press. On February 7, he began a hunger strike to protest his continued detention at Tura prison.<p>
<i>Socialist Worker</i> received the following request for messages of solidarity:<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">EGYPTIAN SECURITY Forces arrested three activists of the Socialist Studies Center during their presence in the Cairo International Book Fair, where the Socialist Studies Center was exhibiting its publications, among which was their recent release on change in Egypt titled <i>The Socialists' Route to Change: A Socialist Vision for Change in Egypt</i>. <p>
We have received information that the three activists have been transferred to Madinet Nasr Police Station, where they will be summoned to the evening prosecution and charged with distribution of leaflets denouncing a new presidential mandate for Hosni Mubarak, or the passing on of the presidency to Gamal Mubarak, in addition to calling for the February 4 demonstration.<p>
The demonstration has been called for by the Popular Committee for Change, which calls for constitutional change enabling Egyptians to elect their president from among more than one candidate, reducing the authority of the president, an end to the emergency state, and the release of detainees and prisoners of conscience. Those demands have been endorsed by 11 political groups, 15 popular committees and organizations, and more than 2,000 public figures, university professors, journalists, writers, artists, trade unionists, lawyers, engineers and students. <p>
Among the three detained activists are a woman lawyer as well as journalist Ibrahim al-Sahari, who was brutally beaten by the police. <p>
