<p><font face="Times New Roman, Times, serif" size="4"><b>University of California-Santa Cruz</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Terrance Wilson</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SANTA CRUZ, Calif.--More than 100 clerical workers, faculty and students demonstrated January 27 in support of the 600 clerical workers at the University of California-Santa Cruz (UCSC) and the 17,000 clericals in the entire UC system. The protesters marched directly into the Cowell College conference room where bargaining was taking place. <p>
This followed a protest of more than 400 clerical workers, faculty and students last November to demand better wages and benefits.<p>
UC workers struggle to get by in Santa Cruz County, the fifth-most expensive county to live in the country. To afford a two-bedroom apartment here, a worker needs to earn at least $53,872 a year, according to the National Low Income Housing Coalition. <p>
But more than half of the clerical workers at UCSC make less than $31,000 per year, and they haven't had a raise since May 2003. Because Santa Cruz has been classified as a "rural" area, clerical workers in certain departments at UCSC earn 15 percent less than clerical workers at other UC campuses. <p>
Workers, faculty and students need to keep up the pressure on the UC Regents to get a decent contract for UCSC clerical workers.<p>
