<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">February 11, 2005 | Pages 10 and 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Indiana and Missouri state workers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AS MANY as 50,000 state employees in Indiana and Missouri saw their right to collective bargaining stripped away with the flourish of a pen last month.<p>
In Indiana, newly elected Republican Gov. Mitch Daniels rescinded previous executive orders that provided collective bargaining rights for approximately 25,000 state employees. That means that automatic collective negotiations for pay, benefits and work rules with state officials will no longer happen for some 8,600 American Federation of State, County and Municipal Employees (AFSCME) workers--including hospital attendants, welfare caseworkers and professional health care workers--and some 14,000 workers represented by the "Unity Team," a combination of the United Auto Workers and the American Federation of Teachers. Daniels also rescinded contracts of state workers negotiated through 2007.<p>
In Missouri last month, newly elected Republican Gov. Matt Blunt also rescinded the collective bargaining rights of about 25,000 state workers represented by AFSCME and the Service Employees International Union. That includes some 9,000 patient care workers in state mental hospitals, craft and maintenance workers and other workers represented by AFSCME who had reached contracts--which Blunt says are now null and void--with the state through 2006.<p>
Both Blunt and Daniels claim that the changes are necessary in order to make up state budget deficits--an ominous sign that, in addition to severe budget cuts, state workers will likely be facing layoffs and cutbacks in the months to come. As Irene Hansen, a Unity Team member who works at the Indiana State Library, told the <i>Indianapolis Star</i>, "The union is the only safeguard against management taking advantage of employees. This turns back the clock in Indiana." <p>
