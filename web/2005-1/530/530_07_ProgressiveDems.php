<font face="Times New Roman, Times, serif" size="5"><b>Offering a lifeline to the Democrats</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 11, 2005 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">ONE OF the end results of failed presidential campaigns is that they leave behind a vehicle for supporters to remain active between elections. The failed Democratic presidential campaigns of Dennis Kucinich, Howard Dean and Al Sharpton have given us just such a formation--the Progressive Democrats of America (PDA).<p>
The Progressive Democrats appeared on the scene when they hosted a conference during the Democratic National Convention in Boston. <p>
Staffed predominantly by organizers from the Kucinich campaign, the PDA proclaims its goals as: "We are specifically committed to the realization of new models for achieving local, national and global security that redirect the current wasteful and obscene levels of military spending toward the uncompromising and effective funding of: health and education programs; an end to discrimination; the provision of full and meaningful employment; and an end to poverty for all people. To achieve these goals, we dedicate ourselves to work within the general framework of the Democratic Party and with sister organizations to create a new, democratic, grassroots-based, nationally federated organization."<p>
The PDA is currently engaged in the campaign (now turning into a coronation) to win Howard Dean's nomination as chair of the Democratic National Committee.<p>
Since the PDA draws its inspiration from the Kucinich campaign, it's worth remembering what role Kucinich played in 2004. His campaign remained alive long after John Kerry had secured the nomination so that Kucinich could carry the campaign's progressive and antiwar message into the Democratic convention.<p>
Or so he said. Kucinich folded on all of his essential positions before the convention began. And when he got his chance to address the convention, he used it to give an uncritical endorsement to Kerry.<p>
The PDA is a lot like the Kucinich campaign. Its positions sound good on paper, but it's aiming to influence a political party that is increasingly hostile to the majority of what it stands for. And since "progressives" don't have the numerical or financial clout to really influence the Democrats where it counts, the PDA threatens to become yet another small pressure group issuing position papers that the party leadership ignores.<p>
Some activists who voted for Kerry have concluded that their time would have been better spent building the antiwar movement or supporting a genuine third party alternative.<p>
That's where the PDA comes in. It offers a lifeline to the Democrats from people who, in the wake of the election debacle, might decide to give up on the party altogether.<p>
Unfortunately, people with a history of supporting alternatives to the Democrats--like global justice campaigner Medea Benjamin and other leading members of the Green Party--are following up their support of Kerry to stop Bush in 2004 by promoting the PDA.<p>
In a post-election interview with the <i>Progressive</i> magazine, Benjamin correctly pointed out: "This election has not been good for third party politics in general. We didn't come out of this campaign with a strong sense among progressives of the need to build a third party." But Benjamin--who also proclaimed in a <i>Nation </i>article that she'd given up on supporting the Democrats in the future--headlined the PDA's conference earlier this month.<p>
The same confusion is evident in a pre-election letter promoting the PDA from activist and politician Tom Hayden. "We are the only 'advanced' country without a solid liberal-left bloc," Hayden wrote. "It makes us bleed. Without a left, liberalism loses its spine. We need to push hard for a Progressive Democratic Movement inside and outside the party."<p>
So the ultimate point of the building the left is to give liberalism a backbone? When it counted in 2004, the majority of left intellectuals and activists either echoed the Democratic campaign or fell silent on huge points of difference between Kerry and the independent left.<p>
Are the Democrats an obstacle to which progressives must build an alternative? Or can the Democrats be "reclaimed" for progressive politics? If the left doesn't get these questions right, we risk repeating the debacle of 2004 on an even greater scale in the future.<p>

