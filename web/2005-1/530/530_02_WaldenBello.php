<font face="Times New Roman, Times, serif" size="5"><b>Support Walden Bello</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 11, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">GLOBAL JUSTICE activists Walden Bello and other leaders of the movement in the Philippines are under the threat of death--not from right wingers or the state, but by leaders of the Communist Party of the Philippines. At its convention last weekend, the International Socialist Organization passed a resolution, printed below, expressing support for Bello and the others.<p>
Read more about the <a href="http://www.focusweb.org/main/html/Article565.html" target="_blank">defense campaign</a> at the Web site for Focus on the Global South.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">TO WIN the struggle against war and imperialism, the international left needs unity, democratic discussion and debate. Far from promoting these fundamental principles, the Communist Party of the Philippines (CPP), in the December 7, 2004 issue of its principal organ, Ang Bayan ("The Nation"), has branded key activists in our movement--including Walden Bello, Liddy Nacpil, Etta Rosales and Popoy Lagman--as counterrevolutionaries. The article is accompanied by a chart purportedly showing the <a href="http://www.philippinerevolution.org/cgi-bin/abshow/abshow.pl?year=2004;month=12;day=07;edition=eng;article=08" target="_blank">"Links of counterrevolutionary groups with Trotskyites and Social Democrats."</a> Lagman has already been assassinated, reportedly by the CPP's armed wing, the New People's Army.<p>
Walden Bello, Executive Director of Focus on the Global South, is one of the most articulate foes of imperialism and corporate globalization. Liddy Nacpil is a leading human rights activist. Etta Rosales is head of the human rights committee of the Philippine House of Representatives. They are comrades in struggle, not counterrevolutionary agents.<p>
The International Socialist Organization condemns the CPP's threats against these and other genuine activists. The CPP's sectarian actions divide our ranks and weaken our cause. Political differences should be resolved through democratic debate, not by censorship under penalty of death.<p>

