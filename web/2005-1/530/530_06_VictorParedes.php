In taking a stand against the war, Pablo joined a growing number of military resisters--including Staff Sgt. Camilo Mejia, who is serving one year of hard labor for refusing to return to his unit in Iraq after a furlough, and Sgt. Kevin Benderman, who refused deployment to Iraq in January.<p>
Now, a defense campaign is being organized to help Pablo. Here, his brother, VICTOR PAREDES, talks to <i>Socialist Worker's </i>GEOFF BAILEY about the case.<p>

<b>TELL US about Pablo's case.</b> <p>

PABLO'S SHIP received orders to transport 3,000 Marines and equipment to the Persian Gulf on December 6. At that point, my brother had a significant objection to those orders because he knew he was contributing to an act of aggression. <p>
He knew there were a lot of lives being lost, and there was a lot of questioning about the justification for the war. So he decided to disobey the order to board. He was very public about it and invited a lot of media there when he did so. He explained his reasoning and has been explaining it ever since. He began to search out legal assistance and counseling. <p>
On December 18, he felt confident that he had gotten some coverage, had his counsel in place and was prepared to submit his conscientious objector (CO) status. At that time, he turned himself in to the naval authorities. <p>
Up to that point, the Navy had been declaring him a deserter. When he turned himself in, he was put in legal holding while his CO status was being reviewed. It's kind of like being on parole. He has orders to go to the base from 7 a.m. to 3 p.m., and he has weekends off. <p>
We're waiting to hear whether they accept his CO application, or whether they are going to charge him. <p>

<b>IF PABLO'S application is denied, what charges might he face?</b> <p>

WE EXPECT that most likely, he will be charged with "unauthorized absence" and "disobeying an order," and possibly "missing ship's movement." None of those are terrible offenses: he could receive a few months in military confinement and would be discharged in some way--probably less than honorably, but not dishonorably. <p>
But the military has a range of ways of dealing with this. They can keep it disciplinary, which would carry very few repercussions, or it can go all the way up a special court-martial. It can be as bad as a year and as bad as dishonorable discharge. But we don't think that will be the case.<p>

<b>WHY DID Pablo join the Navy, and what led up to Pablo's decision to refuse to ship out?</b> <p>

HE'S GOTTEN to the point where he's opposed to war, period. We grew up in an inner-city setting, a tough setting. You learn to survive. <p>
We had a Catholic upbringing, and there were certain values that we had growing up. But you made sure that you survived--you made sure you got out. When the military came around, he saw an opportunity to go to school and find some ways to make some money while he was doing it.<p>
Slowly but surely, he began maturing and questioning things--questioning what he believed and what he had seen. <p>
The most pivotal point was when he was assigned in Japan aboard the <i>USS O'Brien. </i>He spent two years in Japan, and in Japan, he was able to see a society that goes a different route--community service, a police force that doesn't need weapons--a whole different philosophy. <p>
He started reading, exploring everything from philosophy to politics to theology, and he came to the conclusion that he felt that we should understand that we are part of a bigger community--that we're human. War is counterproductive, because you're causing mass damage to yourself.<p>

<b>THERE ARE some who argue that since Pablo volunteered for the military, he has no right to question his orders. How do you respond to that?</b> <p>

WE SAY that our largest responsibility is citizenship, and our largest responsibility is to humanity. Just because you become a soldier doesn't mean you become a robot. That's why things like conscientious objector status exist. <p>
When you take your [military service] oath, you're told that when an order you receive is illegal and immoral, you not only have the right, but the obligation, to disobey it. This was the outcome of Nuremburg Trials at the end of World War Two--the reason German officers found themselves in so much trouble. They could have made the same argument: "Hitler was giving us some order, so we just obeyed them." But nobody absolved them of that, and the reverse is true as well. <p>

<b>HOW IS Pablo's case connected to other recent cases of resistance within the military? And how is it related to the wider effort to end the war?</b> <p>

I THINK that while the motives may be different in each case, the connectedness comes from overall resistance to war. <p>
In Kevin Benderman's case, he actually saw combat in Iraq and decided that it was wrong. In Camilo Mejia's case, he saw the abuses that were taking place. In my brother's case, he didn't believe that we should be harming ourselves. So there is connectedness that on some level, they are all making a connection that human life is worth a lot more than this war. <p>
With respect to Iraq specifically, there have been close to 6,000 cases of desertion since the beginning of the war. We can either assume that there are 6,000 people who are crazy, or we can assume that there is something wrong. <p>
Even the most conservative opinion show that 51 percent of the country is against the war. Again, you can choose to believe that either 51 percent of the country is crazy, or that there is something to question.<p>
People need to understand that massive loss of life is a disaster, which we should also question. I'm not necessarily advocating that you should be for or against it, but make sure that we question it. <p>
Because if 9/11 made us move heaven and earth to find out what went wrong and why we lost close to 3,000 lives on that day, wouldn't we call into question why we purposely put more than 3,000 lives in harm's way every day in Iraq?<p>
Most conservative estimates put Iraqi casualties at around 30,000, and more realistic estimates put it above 100,000. What makes that okay? What makes people run to the aid of those devastated by the tsunamis--which they should--but we turn our heads when it comes to this?<p>
When I went to school, we learned that when you killed this many people of a particular race or nationality, it was called genocide. I'm not very good at math, but I don't know how genocide and greed can equal "Enduring Freedom." Life is a lot more valuable than that.<p>

<b>HOW CAN people help in Pablo's case?</b> <p>

ONE OF the biggest things is to understand that you yourself can make a difference. Send letters to your representatives. Let them know how you think--in favor or against. Make it known how you feel. <p>
For my brother specifically, we are in the process of raising funds, so any donations are very appreciated. Statements of support and petitions like the ones that we've been using will help. <p>

