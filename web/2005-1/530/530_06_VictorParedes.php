<font face="Times New Roman, Times, serif" size="4">Brother of military resister Pablo Paredes speaks out:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Human life is worth more than this war" </b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 11, 2005 | Pages 6 and 7</font><p>

<font face="Times New Roman, Times, serif" size="3">ON DECEMBER 6, 2004, Petty Officer Third Class Pablo Paredes was scheduled to ship out from San Diego for Iraq on the <i>USS Bonhomme.</i> Instead, he showed up in a T-shirt that read, "Like a cabinet member, I resign." <p>
Pablo held a press conference to explain his opposition to the war and occupation in Iraq. "I can't sleep at night knowing that's what I do for a living," he said. "I'd rather do military prison time than six months of dirty work for a war that I and many others do not support."<p>

