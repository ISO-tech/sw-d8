<font face="Times New Roman, Times, serif" size="5"><b>Marine general: "It's fun to shoot people" </b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 11, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">"IT'S A hell of a hoot. It's fun to shoot some people." Those were the words of Lt. Gen. James Mattis, the Marine Corps general who led 65,000 troops during the conquest of Baghdad two years ago, as he spoke last week to 200 people at a San Diego forum about the tactics of fighting the "war on terrorism" sponsored by top U.S. defense contractors.<p>
"Mad Dog," as he's known to his Pentagon colleagues, told an enthusiastic crowd, "I'll be right up front with you, I like brawling. You go into Afghanistan, you've got guys who slapped women around for five years because they didn't wear a veil. You know, guys like that ain't got no manhood left anyway. So it's a hell of a lot of fun to shoot them."<p>
The Pentagon isn't planning on reining in Mattis. "Lt. Gen. Mattis often speaks with a great deal of candor," Gen. Michael Hagee told a reporter. "I have counseled him concerning his remarks, and he agrees he should have chosen his words more carefully."<p>
But the problem isn't that Mattis failed to choose his words "carefully." It's that he revealed how little value the Bush administration places on the lives of Arabs and Muslims.<p>
As Nihad Awad, executive director of the Council on American-Islamic Relations, said in a statement, "We do not need generals who treat the grim business of war as a sporting event."<p>

