<font face="Times New Roman, Times, serif" size="4">Bush's new budget:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Billions for war, cuts for the poor</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 11, 2005 | Page 1</font><p>

<font face="Times New Roman, Times, serif" size="3">GEORGE W. BUSH'S budget proposal will impose deep cuts in domestic spending, while showering money on the military and homeland security.<p>
Bush claimed that his principle in deciding on the cuts was that "a taxpayer dollar must be spent wisely, or not at all." To judge from a budget that will reduce or eliminate funding for some 150 government programs, the administration must think that it's "unwise" to spend anything on helping the poor or vulnerable in U.S. society.<p>
"A cut of this magnitude will force communities to close youth centers, curtail neighborhood revitalization programs, help fewer elderly homeowners stay in their homes, leave poor neighborhoods without water and sewer services, and reduce or eliminate a host of other activities," said Sheila Crowley, president of the National Low Income Housing Coalition.<p>
The Bush budget will cut all discretionary spending--that is, funding for all programs except entitlements, like Social Security and Medicare--by about 1 percent. All discretionary spending except for the Pentagon, of course--which will get a 5 percent hike.<p>
Not all military spending will go up, though. For all his talk about "supporting the troops," Bush wants to double the co-payment charged to many veterans for prescription drugs--and impose a new fee of $250 a year for veterans to use the government's underfunded and understaffed health care system.<p>
Administration officials told reporters that they made "tough choices" to rein in a huge and growing government deficit. But the truth is that the cuts in discretionary spending will save no more than $15 billion--because these programs only represent about 20 percent of the government budget.<p>
Put another way, cutting money from programs like home heating assistance for the elderly, veterans' benefits, job training programs or child care assistance for low-income families doesn't save much money--because the government doesn't spend much on these programs to begin with.<p>
The Bush plan is supposed to cut the government deficit--which last year reached a new record of $412 billion. But it won't. That's because the Bush budget doesn't include money for two top priorities in Bush's State of the Union address--the war in Iraq and privatizing Social Security. Neither the $80 billion requested last week by the administration in supplemental spending for the Iraq occupation, nor the estimated $1 to $2 trillion in "transition costs" for Social Security privatization shows up in the budget.<p>
The real source of the record government deficit isn't spending on social programs, but the huge tax cuts for the rich and skyrocketing military spending pushed through by the Bush administration. But the Republicans aren't solely to blame. Congressional Democrats gave Bush the margin of victory on both of his major tax cut packages in his first term. <p>
And now, the Democrats' main complaint about a variety of Bush initiatives--from Social Security privatization to the new budget--is that they will increase the deficit. By taking up the former battle cry of Republican deficit hawks, the Democrats are giving political cover to Bush's bloated spending on the military and giveaways to the superrich and corporations.<p>
Now that Bush has created a budget crisis, he claims that he wants to "fix" the mess--by cutting Medicaid, home heating assistance for the elderly, veterans' benefits and much more. His real agenda is to fatten the bottom line for corporations and the wealthy--and make the rest of us pay the price.<p>

