<font face="Times New Roman, Times, serif" size="4">Salinas chooses police over public libraries</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>An insult to Steinbeck's memory</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 28, 2005 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
Why should a rich moron like George W. Bush get to go to Yale University while millions of migrant workers live stunted lives, their children dying from malnutrition and exposure to pesticides?<p>
John Steinbeck, the great class-conscious California writer, asked--and answered--this question in his many novels and stories. "The monster," as he called capitalism in <i>The Grapes of Wrath</i>, "has to have profits all the time."<p>
Champion of the downtrodden--especially the hard-luck migrant workers who made his beloved California Central Valley bloom without sharing in its bounty--Steinbeck believed deeply in the transformative power of knowledge. "I guess there are never enough books," he once said.<p>
How disgustingly ironic, then, that Salinas, Calif.--Steinbeck's hometown and today, a predominantly Latino farming community--should be on its way to becoming the largest American city without a public library. <p>
The excuse? In a state where energy, telecommunications and agribusiness bosses count their profits in the tens of billions of dollars, no one can come up with $8 million to close the Salinas city government's budget shortfall. So a town with libraries named for Steinbeck and labor leader Cesar Chavez is planning to close all its public libraries by June.<p>
What would Steinbeck have said? The same thing he said about the starvation amid plenty that characterized the Central Valley in the 1930s: "The failure hangs over the State like a great sorrow." Every day, more than 2,000 people visit Salinas's libraries, the great majority of them farmworkers and their children. <p>
Mayor Anna Caballero and most of the town council want to make sure the town has enough money for their favorite constituency--the police. Yet Amanda Holder, spokeswoman for the city's National Steinbeck Center, spoke for many when she speculated recently, "Perhaps if there were more libraries, we wouldn't need so many police."<p>
People in Salinas and around the country are reacting to the planned closures with anger. Mayor Caballero was jeered at a recent Save the Libraries vigil, when she told residents to "stop complaining and contribute." At that, residents started throwing money at her in disgust. <p>
Activists have a difficult fight on their hands to save the Salinas libraries. But as Tom Joad puts it near the end of <i>The Grapes of Wrath</i>: "I been wonderin', if all our folks got together..."<br>
<b>David Rapkin, </b>Los Angeles<p>

