<font face="Times New Roman, Times, serif" size="4"><i>The Woodsman </i>movie falls short </font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The illness he fights every day</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Elizabeth Lalasz</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | Janury 28, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>The Woodsman, </b></i>directed by Nicole Kassell, starring Kevin Bacon and Kyra Sedgwick.</font><p>

<font face="Times New Roman, Times, serif" size="3"><i>THE WOODSMAN</i> is a thought-provoking movie, based on a stage play by Steven Fechter and adapted by first-time director Nicole Kassell, about a pedophile's struggle to adjust after being released after 12 years in prison.<p>
Walter (played by Kevin Bacon) moves back to his hometown at the age of 45. He gets an apartment across from a grade school playground, because that is the only landlord who would rent to an ex-convict. He gets a job in a lumberyard, only because the owner's father thought he did good work, and he takes the bus back and forth each day. His life is like that of many workers--but with the agony of his own affliction.<p>
The only person in his family who will visit him is his brother-in-law, although Walter makes it plain that he wants to see his sister and niece. And other than this, Walter leads an isolated life, until he starts a relationship with coworker Vickie (Kyra Sedgwick).<p>
The strength of this film is its portrayal of Walter as not inherently evil, but as an ordinary man with a mental illness that he fights every day. It's from this starting point that <i>The Woodsman</i> exposes all the brutality and vilification of pedophiles within our society.<p>
When a coworker finds Walter's picture on a sex offenders' list on the Internet, she prints it out and puts it in his locker, exposing his past to his coworkers. They react with anger and hatred. Several times, Walter's parole officer (played by Mos Def) visits his home and tells him he's lower than scum and he'd kill him if he had a chance.<p>
And the court-appointed psychologist, who Walter sees once a week, provides little help to Walter other than making him keep a diary and suggesting that his relationship with his sister is the cause of his pedophilia.<p>
The problem with <i>The Woodsman</i> comes when it moves away from this focus and portrays Walter as transforming from a "bad" to "good" pedophile--a change that feels oversimplified to prove a point. It's as if the director becomes afraid of appearing too sympathetic to her character Walter, so she tries to have it both ways.<p>
During a difficult-to-watch part of the movie, Walter begins to follow a 12-year-old girl after work and almost assaults her, before his conversation with the girl triggers something that makes him stop. The conclusion seems to be that Walter sees the error of his ways by chance--being at the right place at the right time.<p>
He puts his revelation into action by beating up another pedophile. Worse, he's congratulated--in a backhanded way--by his hostile parole officer, which implies there's hope for Walter after all.<p>
So, what starts out as a provocative film about how society looks at and treats pedophiles seems to conclude that there can be "good" and "bad" pedophiles, who perhaps can cure themselves to become more "acceptable" members of society.<p>
This is a serious weakness in a courageous project that not many in Hollywood would touch these days in the era of mindless, fast-action Hollywood thrillers. <i>The Woodsman</i> wants to remind us that child molesters are not simply monsters, but deeply troubled human beings, which makes for a hard film to watch.<p>

