<font face="Times New Roman, Times, serif" size="5"><b>HRC is justifying surrender on gay marriage</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 28, 2005 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
So the Human Rights Campaign's (HRC) senior communications manager, according to his letter to <i>SW</i>, believes that the organization's commitment to marriage equality is "rock solid" ("HRC's agenda is rock solid," January 7).<p>
No matter what their intentions, individuals and organizations have to be judged not on what they say about themselves, but on what they actually <i>do</i>. The HRC threw away an "unprecedented $8-$10 million," according to their own Web site, on the 2004 elections to support the campaigns of such anti-gay marriage Democrats as John Kerry and Illinois Sen. Barack Obama--both of whom went out of their way throughout their campaigns to emphasize their opposition to marriage equality. <p>
When activists from Equality California and DontAmend.com tried to get HRC on board to mobilize a cross-country caravan and protest for same-sex marriage rights in Washington, D.C., in the fall, HRC--the nation's biggest and wealthiest gay-rights organization--said its people were too busy working on the Kerry campaign.<p>
Hilary Rosen, the new interim director of HRC, was quoted after 11 states passed anti-gay marriage referenda, "This election may have shown us that the change agents for gay marriage are looking too much like a noisy red Ferrari speeding down quiet Main Street." Sounds like justification for surrender to me. <p>
The HRC shouldn't spit at activists, and then insist it's raining. Supporting politicians who oppose your agenda has proven once again to be political suicide. To win this fight, activists need to mobilize around same-sex marriage as the civil rights issue that it is--and hit the streets. <br>
<b>Sherry Wolf, </b>Chicago<p>

