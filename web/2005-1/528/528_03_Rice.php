<font face="Times New Roman, Times, serif" size="5"><b>Democrats roll over for Rice hearings</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 28, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3"><i>"The Iraqis lack certain capacities."<p>
"I do agree that the tsunami was a wonderful opportunity...And I think it has paid great dividends for us."<p>
"I think it's extremely unfortunate that the Chavez government has not been constructive...We are very concerned about a democratically elected leader who governs in an illiberal way."</i> <p>
<br>
IRAQIS ARE incapable of governing themselves, the tsunami disaster was an "opportunity," and Venzuela's Hugo Chavez ought to be gotten rid of.<p>
These were just a few of the highlights from Condoleezza Rice's confirmation hearings before the Senate Foreign Relations Committee. And despite the media's claim that Rice got a grilling, the committee--Democrats and Republicans alike--lapped it up.<p>
Sen. Dianne Feinstein (D-Calif.) set the tone by complimenting Rice for having the "skill, judgment and poise to lead in these difficult times."<p>
Rice managed to dodge whatever questions she wanted to--from the administration's lies about Iraq's nuclear weapons capabilities to what she thought about the torture at Abu Ghraib. Much was made of Sen. Barbara Boxer's (D-Calif.) questioning--a "heated exchange that electrified the hearing room," according to one report--but it was a blip during a hearing on a confirmation that was a sure thing to begin with.<p>
John Kerry was characteristically dull, his contribution--like the challenge he posed during the presidential campaign--limited to criticism of <i>how </i>the U.S. went to war. "We went in to rescue Iraq from Saddam Hussein," said Kerry, who along with Boxer voted against Rice's confirmation. "Now I think we have to rescue our policy from ourselves."<p>
Then Kerry proceeded to use his question time to whip up even more hysteria about weapons of mass destruction--in North Korea, Russia and even Pakistan. If there was a coup in Pakistan, Kerry warned, "You could have nuclear weapons in the hands of Islamic extremists."<p>
Sen. Barack Obama (D-Ill.), the Democratic Party's new darling, did Kerry one better--asking why Sudan wasn't on the list of U.S. enemies in the "war on terror."<p>
Further proof that the Democrats are every bit as committed to the "war on terror" as the Republicans. They may disagree about the details. But that's not opposition, it's an agreement in principle.<p>

