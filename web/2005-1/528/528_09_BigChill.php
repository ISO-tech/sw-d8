<font face="Times New Roman, Times, serif" size="5"><b>Exposing the liar-in-chief</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Cindy Beringer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | Janury 28, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Dennis Loy Johnson, <b><i>The Big Chill: The Great, Unreported Story of the Bush Inauguration Protest. </b></i>Melville House Publishing, 2004, 109 pages, $8.95.</font><p>

<font face="Times New Roman, Times, serif" size="3">"NORMAL-LOOKING people gone ugly with outrage." That's how Dennis Loy Johnson describes the protesters who came out for George W. Bush's last inauguration in his short book <i>The Big Chill: The Great Unreported Story of the Bush Inauguration Protest.</i><p>
The press is the clear villain in the book, as it failed to accurately report the size and anger of the opposition and ignored the unprecedented military and police presence. <i>The Big Chill </i>gathers stories from outraged participants who found themselves marginalized, or non-existent, in their local papers.<p>
A tradition started by Jimmy Carter has the president getting out of his limousine and walking a stretch of the parade route. Weary protesters returned home, according to Johnson, content in the assurance that they had prevented this "man of the people" moment for Bush--only to find the "impossible photo" in their morning papers.<p>
Securely inside a heavily guarded area full of invitation-only guests, almost literally in his own backyard, he had emerged only long enough for the faked photo. Johnson wonders what would have happened had the opposition to Bush been taken more seriously by the press.<p>

