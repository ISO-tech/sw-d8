<font face="Times New Roman, Times, serif" size="5"><b>Refusing to fight in Bush's war</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 28, 2005 | Pages 1 and 2</font><p>

<font face="Times New Roman, Times, serif" size="3">KEVIN BENDERMAN said no. After what he saw during his first tour of Iraq in 2003, Benderman told his Army superiors that he's not going back.<p>
On December 28, the 40-year-old Bradley Fighting Vehicle mechanic with nine years in the Army notified his commanders that he was going to file an application for conscientious objector status--just days before his unit was to be deployed to Iraq.<p>
But the Army considers Benderman AWOL--absent without leave. On January 19, Benderman was charged with Article 85 and Article 87 under the Uniform Code of Military Justice--desertion with the intent to avoid hazardous duty and missing his unit's deployment.<p>
He even received a letter from the battalion's chaplain chastising him for his choice not to deploy. "You should be ashamed of the way you have conducted yourself," wrote the chaplain, Capt. Matt Temple. "I certainly am ashamed of you."<p>
But Benderman refused to back down--and is demanding a general court martial. "I am ashamed to be associated with this mess, and I certainly did not join the Army to kill women, children and old men," Benderman told a reporter. "I just don't see how these innocent people could be a threat to the constitution of the United States."<p>
In a letter to George W. Bush dated November 20, 2004, Benderman wrote, "If you truly had respect for the military and the people that serve, then you would not continue to kill them in your war...I want to fulfill my contract that says I joined the Army to protect my country against all enemies foreign and domestic, and as far as I am concerned you are a domestic enemy of the United States."<p>
There are more soldiers like Benderman who are refusing to fight. Seventeen more members from Benderman's 2-7 Infantry Battalion at Fort Stewart, Ga., are refusing to deploy to Iraq and are also requesting general courts-martial.<p>
Others have resorted to more desperate measures. Two members of the 2-7, specialists J.R. Burt and David Beals, tried to commit suicide in early January. They are being held in the psychiatric ward of Winn Army Hospital, where they are under constant harassment, Army sources who asked to remain anonymous told Robert Finnegan of the <i>Southeast Asia News.<p>
</i>Several witnesses say that before Beals' suicide attempt, a 2-7 platoon sergeant told him, "When I get you to Iraq, I'm going to get you killed."<p>
The Bush administration boasts that it supports its troops. Soldiers like Beals and Burt show that this is a lie.<p>
The antiwar movement needs to rally support behind soldiers like Benderman who refuse combat. During the Vietnam War, the growing number of soldiers who broke discipline and refused to fight played a key role in forcing the U.S. to withdraw.<p>
Soldiers resisting this unjust war deserve the firm support of the antiwar movement and veterans' organizations. We say: Stop Bush's barbaric war on the people of Iraq and bring the troops home now.<p>

<font face="Times New Roman, Times, serif" size="4">Kevin Benderman on the U.S. war on Iraq:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Putting children through torture"</b></font><p>

<font face="Times New Roman, Times, serif" size="3">KEVIN BENDERMAN spoke to <i>Socialist Worker </i>about his opposition to the war in Iraq.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>WHAT TURNED you against the war?</b><p>

THE BIGGEST thing that convinced me was seeing a young girl standing on the side of the road with her arms burned all the way up to her shoulders. These were third-degree burns, and her arm was black in some places.<p>
You have to wonder why mankind can't figure out a better way to solve its differences other than putting innocent young children through that kind of torture and agony.<p>
I stood at the edge of mass gravesites, and I saw old men, women and children in those graves.<p>
We were in an old customs building while I was with the Fourth Infantry Division. Before we had a chance to put up the concertina wire, kids would climb up a wall, look at us, laugh and throw little rocks. Then the captain came out and said, "If they get back up on that wall, you guys shoot those kids."<p>
It was obvious that these were children. It's not like they were grown men trying to throw pieces of concrete. They were just kids. Of course, we looked at the captain like he was crazy.<p>

<b>YOU'VE BEEN in the military for years, but now you've been charged with desertion.</b><p>

<font face="Times New Roman, Times, serif" size="3">I'M SURPRISED that they're charging me with desertion, considering that I haven't gone anywhere. And I've done everything they said for me to do, so I'm not a deserter. I've just refused deployment.<p>
I'm prepared to follow through on my beliefs. I have to be able to accept whatever consequences come from my actions.<p>
Under Army regulations, they can either assign you to noncombatant duties, or they can discharge you. And I requested a discharge. <p>
I'm scheduled for an Article 32 hearing on February 7. At this hearing, they decide whether to go forward with a court martial, reduce the charges, impose non-judicial punishment or add charges. They produce evidence to back up their charges, and I can produce evidence to dispute the charges.<p>
