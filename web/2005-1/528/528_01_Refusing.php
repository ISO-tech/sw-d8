<font face="Times New Roman, Times, serif" size="5"><b>Pablo Paredes needs your support</b></font><p>

<font face="Times New Roman, Times, serif" size="3">PABLO PAREDES watched from a San Diego dock as his ship deployed to the Persian Gulf December 6.<p>
Paredes, a petty officer third class, was just 20 months away from finishing his six-year enlistment in the Navy when he declared himself a conscientious objector and refused to ship out for Iraq. "I'd rather do a year in a prison in the military than do six months of dirty work for a war I don't believe in," he told reporters.<p>
Now, the military has branded Paredes a "deserter" and is considering bringing charges against him. But as Pablo said, "Our efforts need to echo in the civilian world, organizations, students, etc., need to speak out to stop this war, stop the violence and cruelty."<p>
His family and supporters have organized a campaign, "Citizens for Pablo," to aid him in his fight. A petition to defend Pablo, sponsored by Citizens for Pablo and the Hunter College chapter of the Campus Antiwar Network (CAN), is being circulated. Visit the Citizens for Pablo Web site at <a href="http://www.swiftsmartveterans.com" target="_blank">www.swiftsmartveterans.com</a> for more information.<p>

