<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">January 28, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>United</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><p>

<font face="Times New Roman, Times, serif" size="3">FLIGHT ATTENDANTS at United Airlines are voting on a proposed labor contract containing massive concessions, despite being given little time by union officials to review or debate the deal.<p>
Members of the Association of Flight Attendants (AFA), affiliated with the Communications Workers of America, are being asked to take a 9.5 percent pay cut, reduce vacation pay and cut bonus pay for hours worked on short-staffed flights. The contract would increase pay 2 percent per year in the last three years of the proposed five-year contract, but this would still leave wages 3.5 percent lower in dollar terms--and even more once inflation is taken into account.<p>
"I used to be proud to say that I was a flight attendant for United Airlines," a 20-year veteran of the company said. "Now they're taking everything away."<p>
A series of other cuts will eat into flight attendants' pay as well, such as the elimination of reimbursement for uniform cleaning and the cancellation of premium pay for flight pursers and attendants with foreign language skills.<p>
AFA leaders are presenting the deal as a partial victory, because their threat to implement job actions known as CHAOS forced United to back off on demands to terminate the pension plan and force flight attendants to pay for huge increases in health care costs. They argue that there was no alternative, given that United is seeking a federal bankruptcy judge's approval to abrogate labor contracts altogether.<p>
The pilots' and mechanics' unions have also reached tentative agreements with the company, although a judge set aside the pilots' deal for now. <p>
The AFA, like those unions, also accepts that givebacks are inevitable to help restore United to profitability. "We have been consistent that we could not give United management one dime more than was necessary," AFA leaders wrote in a contract summary.<p>
Yet more givebacks may be in the offing. The union acknowledged that the final draft of the contract won't be completed for another 60 days--meaning that a "yes" vote by AFA members would give officials a blank check in further negotiations. And given that the concessions follow an earlier cut in pay, some United flight attendants say enough is enough.<p>
"I'm voting no," said one attendant who works international flights from Chicago. "This is too much."<p>

