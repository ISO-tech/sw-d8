<font face="Times New Roman, Times, serif" size="5"><b>What the U.S. has in store for Iran</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | Janury 28, 2005 | Page 5</font><p>

<font face="Times New Roman, Times, serif" size="3">A FEW hours before President George Bush gave his inauguration address promising to rid the world of tyranny, Vice President Dick Cheney made a few comments that tell more about where the administration is heading.<p>
Yukking it up with right-wing talk show host Don Imus on MSNBC, Cheney placed Iran at the "top of the list" of "potential trouble spots" that the Bush administration was determined to deal with in its second term. Of course, added Cheney, Israel "might well decide to act first" to destroy Iran's nuclear capacity and "let the rest of the world worry about cleaning up the diplomatic mess afterwards."<p>
For someone who normally plays the role of the administration's Prince of Darkness, Cheney's concluding calls for a diplomatic solution seemed out of character. But it could all be an elaborate setup for what Bush, Cheney, Defense Secretary Donald Rumsfeld and every other neoconservative in Washington wants--a decisive confrontation with Iran leading to "regime change." <p>
Just such a scenario appeared in print days before the inauguration, when investigative journalist Seymour Hersh revealed in the <i>New Yorker</i> a secret Pentagon operation deploying commando units into Iran to develop intelligence intended to support U.S. strikes against Iranian nuclear facilities.<p>
With that background in mind, it's easier to understand where Cheney's calls for diplomacy come in.<p>
Iran, a signatory to the Nuclear Nonproliferation Treaty, publicly acknowledged that it has an active nuclear research program intended to create a nuclear power industry. It insists that it isn't interested in developing nuclear weapons. But observing what the U.S. did to Iraq--another member with Iran and North Korea of the "axis of evil" Bush named in 2002--it would hardly be irrational for Iranians to develop nukes as a matter of self-defense.<p>
For several years, a European Union negotiating team led by Britain, France and Germany has worked with the International Atomic Energy Agency (IAEA) to persuade Iran to give up its nuclear program. <p>
The Europeans insist they are making progress. But the U.S. hasn't lifted a finger to support this diplomatic road. "The neocons say negotiations are a bad deal," an IAEA official told Hersh. "And the only thing the Iranians understand is pressure. And that they also need to be whacked."<p>
Right now, there's no political support to "whack" Iran among the U.S.'s European allies. But if a U.S. boycott of the negotiations can ensure their failure, then the U.S. can argue--as it did in the lead-up to the invasion of Iraq--that "diplomacy" had been tried and failed.<p>
So Cheney's backing of diplomacy today is merely his recognition that the political ground hasn't yet been established for what he and Bush really want to do. "We're not dealing with a set of National Security Council option papers here," a former intelligence official told Hersh. "They've already passed that wicket. It's not <i>if</i> we're going to do anything against Iran. They're doing it."<p>
Why the obsession with Iran? Why would the U.S. even think about such an adventure with its Iraq occupation a disaster?<p>
Writing in <i>Le Monde Diplomatique</i>, Walid Charara explained: "Behind the ideological window-dressing of the new 'democratic messianism,' there are two main reasons for the Bush administration's uncompromising determination. First, there is Iran's geo-strategic status. It is an independent and middle-ranking regional power that has engaged in military cooperation with Russian and China...[T]his makes it the last bastion still to be holding out against a permanent U.S. takeover of the Middle East. [Second,] Iran is the last surveying ally in the region of those states and organizations still opposed to Israel."<p>
In the coming months, we will be subjected to a barrage of scare stories about the Iranian bomb, Iranian backing for terrorism, Iranian support for the resistance in Iraq--even Bush's delusions about a post-Islamic Republic of Iran. That's why it will be important to remember the real reasons why the U.S. worries about Iran.<p>

