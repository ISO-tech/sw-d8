<font face="Times New Roman, Times, serif" size="5"><b>How the U.S. engineered an undemocratic vote</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | Janury 28, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">IT WILL be easier--and much safer--to vote in the Iraqi elections this month if you live in the northern suburbs of Chicago than if you live in Anbar province south and west of Baghdad.<p>
In protest against the brutality of the U.S. occupation, most of Iraq's Sunni Muslim minority is expected to boycott the vote altogether. And more than half the population lives in areas where U.S. generals admit they can't guarantee that voting will even take place.<p>
With less than a week to go, the locations of polling stations across Iraq were being kept secret--in the hopes of preventing guerrilla attacks. This will mean an immense last-minute effort to get ready for voting.<p>
Compare that to the careful and unprecedented election preparations <i>outside </i>Iraq. Some 1.2 million Iraqi expatriates living abroad are eligible to vote in any of 14 host countries. In the U.S., polling stations have been set up in five cities.<p>
Washington clearly hopes that a strong turnout internationally will help offset expected low participation in large areas of Iraq itself--but reports indicate that participation outside Iraq will be lower than expected.<p>
According to author and Iraq expert Juan Cole, half of all the political parties and all of the individuals who had originally announced that they would participate in the election have since withdrawn--largely as a protest against the way that the elections are being conducted under U.S. occupation.<p>
The main concern is safety. Iraq's armed opposition to the U.S. occupation has escalated its attacks as the election drew closer. According to one report, more members of the recently formed Iraqi security force have been killed in the last month than in the six months before that.<p>
Equally feared by Iraqis is the mayhem that U.S. troops have caused as they try to squelch opposition before the vote.<p>
But for all the force that it has mustered, the Pentagon can't ensure that Iraq won't be engulfed by chaos and violence on Election Day. In fact, international observers will be "observing" the January 30 elections from hundreds of miles away--in Amman, Jordan.<p>
Even Iraqis who don't support the armed resistance resent the U.S. for using them as expendable pawns. "They want me to vote, but they can't protect me," one man told Robert Fisk of Britain's <i>Independent. </i>"Maybe there will be no suicide bomber at the polling station. But I will be watched. And what if I get a hand grenade in my home three days later? The Americans will say they did their best; Allawi's people will say I am a 'martyr for democracy.'"<p>
Iraqis also recognize that the elections have been organized in such a way that there is little at stake. The main vote is for a 275-seat Transitional National Assembly. There are also elections for 18 district councils--and for a separate assembly for Kurds in the three semi-autonomous Kurdish provinces in the north.<p>
The main job of the Transitional National Assembly isn't to govern, but to draft a new constitution--which would then be voted on in a referendum by next October, with new elections to follow within two months. In the meantime, the U.S. would be able to run Iraq as it sees fit.<p>

