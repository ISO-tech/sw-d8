<font face="Times New Roman, Times, serif" size="5"><b>Bribed to promote No Child Left Behind</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | Janury 28, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">SOMETIMES it pays to support the president's policies--literally.<p>
Conservative Black commentator Armstrong Williams revealed earlier this month that he was paid $240,000 by the Department of Education to promote George Bush's No Child Left Behind Act on his syndicated television program.<p>
Under the arrangement, which began in 2003, Williams was required to broadcast two minute-long advertisements in which Education Secretary Rod Paige praised the program. Additionally, a public relations firm hired by the department arranged for Williams "to regularly comment on [No Child Left Behind] during the course of his broadcasts," and stipulated, "Secretary Paige and other department officials shall have the option of appearing from time to time as studio guests." Williams also was required to encourage producers of <i>America's Black Forum</i>--an African American news program--to talk about the law.<p>
Bush and his admirers like Williams consider No Child Left Behind a major step forward for education. That's not what teachers and students are finding.<p>
The law is based around annual tests covering every child in public schools from grades three to eight. If students don't meet strict standards required by the law, their schools go on what teachers are calling "the list." Schools on the list can be forced to fire all their staffs, go "charter," be privatized or be handed over to the state education department. <p>
Even decently funded schools are finding it hard to make the grade--and teachers report that the pressure is intense to focus on teaching students how to test well.<p>
For their second term, the Bush team is gearing up to expand No Child Left Behind by applying its testing standards to high schools as well as grade schools.<p>
Bush publicly announced that he thought Armstrong was wrong to take the bribe to promote No Child Left Behind. But he also added that the Department of Education--which suggested the deal and paid Williams nearly a quarter of a million of taxpayers' money--did nothing wrong.<p>
The administration claims that the deal with Williams was simply about distributing "straightforward" information to minority parents. But as Bryan Monroe of the National Association of Black Journalists said, there was nothing "straightforward" about the payoff to Williams. "I thought we in the media were supposed to be watchdogs, not lapdogs," Monroe said in a statement.<p>

