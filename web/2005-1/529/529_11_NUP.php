<font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The end of the NUP's road to "reform"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 4, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">IS TEAMSTERS President James P. Hoffa the new face of labor movement reform?<p>
Hoffa's recent call for changes in the AFL-CIO is widely seen as the reason for the dissolution of the New Unity Partnership (NUP), the grouping of unions headed by the Service Employees International Union (SEIU) that threatened to pull out of the AFL-CIO unless the labor federation restructured.<p>
The NUP shook up the labor movement when it formed in 2003, presenting sharp criticisms of the AFL-CIO's passivity in the face of declining membership. It proposed devoting more resources to organizing. And it advocated the federation voting to merge smaller and weaker unions with larger ones to create a handful of big unions capable of increasing union density--the percentage of union members in particular industries--which would give labor increased leverage.<p>
But in December, the Teamsters presented their own program for changes in the AFL-CIO, and the leaders of the NUP unions seem to have accepted this in place of their own. <p>
How could the NUP, which couched its proposals in left-wing rhetoric, abandon its program in deference to Hoffa, the very symbol of labor's old-guard conservatism?<p>
First of all, the two proposals aren't that far apart. Like the NUP, Hoffa wants member unions to receive a rebate on dues to the AFL-CIO if the affiliate agrees to spend those funds on organizing new members. This is attractive to any union leader--keeping members' dues under their own control.<p>
Both Hoffa's and the NUP's programs depend on a highly centralized top leadership of unions--at the expense of union democracy.<p>
As for union mergers, Hoffa's Teamsters have already been on a merger binge of their own. Virtually wiped out in their former core industry--freight truck drivers--the Teamsters have absorbed small railroad unions to slow a loss of membership. Another recent merger of desperation was the combination of the United Steelworkers of America with the PACE chemical and paperworkers' union--each of which had earlier absorbed a series of smaller unions to try to stay afloat.<p>
The difference is that SEIU President Andrew Stern and the NUP proposed to bring order to this process with a strategic plan to combine unions in particular industries, rather than create catch-all general unions.<p>
The Teamsters, of course, are exactly that type of one-size-fits-all general union. It represents workers at UPS, factory workers, nurses, meatpackers, government white-collar workers, sanitation workers, airline mechanics and much more.<p>
Likewise, another big critic of the NUP's strategic mergers was the Communications Workers of America (CWA). The CWA isn't opposed to mergers--it has absorbed a series of smaller labor organizations. But rather than the focus on specific industries that Stern and the NUP want, the CWA has used its traditional base in telecommunications as a springboard to pick up members wherever and whenever it can--for example, in the airlines, among government workers, clothing manufacturers and more.<p>
The seeming agreement of the NUP and other union leaders papers over this conflict over how labor should restructure.<p>
The NUP's decision to disband reveals a status quo approach that was always there beneath the left-wing rhetoric. The NUP never questioned labor's subservience to the Democratic Party. And it maintained a diplomatic silence about the recent wave of concessions by some of labor's strongest unions, the worst since the early 1980s. This was not only to avoid offending potential allies among AFL-CIO leaders, but because NUP leaders agree with the dominant idea among labor leaders that unions should promote "partnership" with management--even if that includes accepting concessions.<p>
Now, after initiating a long overdue debate on labor's future, the NUP has agreed to fold before the vast majority of union members have even heard about the issues.<p>
Despite the various proposals on how to organize the unorganized, no labor leader has yet explained how unions will attract new members while they advocate a series of catastrophic concessions in traditional strongholds like the airlines, auto and steel.<p>
There's no telling whether the seeming harmony between the NUP union leaders like Stern and old guard leaders like Hoffa will hold together. But it's clear that neither camp is eager to bring the controversy to the union rank and file, where an open discussion might ignite anger over concessions--and lead to a far more thoroughgoing debate over labor's crisis.<p>

