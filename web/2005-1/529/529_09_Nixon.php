<font face="Times New Roman, Times, serif" size="4"><i>The Assassination of Richard Nixon:</i></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"It's all about money, Dick!"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 4, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>The Assassination of Richard Nixon,</b></i> directed by Niels Mueller, starring Sean Penn, Naomi Watts, Don Cheadle and Jack Thompson.</font><p>

<font face="Times New Roman, Times, serif" size="3">SAMUEL BICKE, a character based on the real-life man who plotted in 1974 to assassinate President Richard Nixon by hijacking a plane and flying it into the White House, is the subject of the new film<i> The Assassination of Richard Nixon.</i> But the real star of the film is the 1970s--the political era that included the Watergate scandal that led to Nixon's impeachment and resignation, the Black Power movement and the unraveling of the myth of the American Dream.<p>
When we meet Bicke, played skillfully by Sean Penn, he is already on his way toward a mental breakdown as he tries to get his life together and win back his ex-wife and family by getting a job as a office furniture salesman.<p>
Early on, his boss/mentor--who urges Bicke to heed the book <i>The Power of Positive Thinking, </i>which claims, "If you believe it, you will receive it"--explains that a well-crafted lie is the secret to success. Pointing to Nixon on a television screen, he asks, "How did he win the 1968 election? Ending the war in Vietnam." What did Nixon actually do? he asks. Escalate the war, and then run on the same promise in the next election.<p>
Bicke can't tolerate lies, from his brother's unscrupulous markup on tires to Nixon's claim that "I am not a crook." What makes Bicke unable to function in society is his inability to ignore any injustice, large or small--the way his Black friend is treated by a white customer, the tiny cocktail dress his ex-wife is forced to wear to work, the big injustice of the American Dream. Bicke can't understand how others can let such everyday injustices pass--nor can he accept the point his wife tries to drive home to him, "I wear this dress, because I have to feed our kids."<p>
Penn's costar in the movie is the TV set, broadcasting the images of the era--ads for the lush Cadillac El Dorado dream car; the game show "The Price Is Right"; footage of Chile's president Salvador Allende, who was overthrown with the aid of the CIA; the FBI's siege on American Indian Movement activists at Wounded Knee; and, of course, the Watergate hearings. This gives a feel for a time when almost everything about society was coming into question.<p>
In one illuminating, and sometimes uncomfortably funny scene, Bicke visits the local office of the Black Panther Party. After seeing Panther leader David Hilliard on a TV talk show, Bicke has concluded that he is in complete agreement with the Panthers' program, but he has a suggestion.<p>
"You're only reaching half of your audience," says Bicke, suggesting that they start a group for all of the people who are feeling the bosses' boot, Black <i>and </i>white. Bicke proposes the name "Zebras," and asks the Panthers politely to think it over, making a $107 cash donation before he leaves the office.<p>
Of course, Bicke is clearly not all there--but it's important to note that a wide audience of other perfectly sane people felt solidarity with the Panthers' fight.<p>
Bicke's excruciating alienation and inability to make any effect on the world around him takes him to the breaking point, which reaches a crescendo in the middle of the office furniture showroom as he screams into the TV screen blaring the Watergate hearings, "It's all about money, Dick!"<p>
On the surface, <i>The Assassination of Richard Nixon </i>may seem to be about one man's deluded response to the world around him. But as the film shows, you didn't have to be deluded to see that something terribly wrong was going on.<p>

