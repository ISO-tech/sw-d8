<font face="Times New Roman, Times, serif" size="4"><b>SCCC Students Against War</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Noah Centenero and Jorge Torres</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--Two weeks ago, more than 300 students staged a walkout organized by Seattle Central Community College (SCCC) Students Against War (SAW). After marching through the halls, students surrounded two Army recruiters and chased them off campus amid chants of "Don't come back!"<p>
Now the school administration is organizing a backlash against our efforts. A few days after the walkout, the school administration threatened that if SAW didn't write an apology to the recruiters, the student group would "cease to be in good standing" for the rest of the quarter.<p>
In SAW's defense, anthropology professor and SAW's student advisor Peter Knutson has written a response to the school administration and forwarded it to all faculty at SCCC, as well as North and South Seattle Community Colleges. <p>
So far, there has been a great showing of support from SCCC faculty. But to fully secure our right to free speech, SAW members are formulating a petition to garner mass support. <p>
We have to continue the struggle. We will not back down to the Army recruiters--and we will not apologize while so many lives are being taken in this war for oil and empire.<p>
