<p><a name="Nader"></a><font face="Times New Roman, Times, serif" size="4"><b>Ralph Nader speaks out</b></font><p>

<font face="Times New Roman, Times, serif" size="3">RALPH NADER is rallying supporters across the U.S. in a 10-city speaking tour.<p>
In San Francisco, 300 people came to hear Nader along with Peter Camejo, his vice-presidential running mate, and Green Party member Matt Gonzales, the former president of the San Francisco Board of Supervisors. Gonzales filled in the numbers for the antiwar chant "Money for schools, not for war," pointing out that so far the city of San Francisco has plowed $520 million into the war effort while the state of California has kicked in $19 billion.<p>
In Seattle, 400 listened as Nader roasted the Democrats for focusing on keeping the Nader/Camejo ticket off of state ballots and refusing to raise the real issues that face most people in the U.S.--the lack of a living wage, jobs, health care and pensions. <p>
And in Boston, 500 people came to an antiwar rally featuring Nader, Howard Zinn and Patti Smith.<p>
Nader's speaking tour seeks to raise money to cover a $250,000 debt racked up by his presidential campaign, which incurred extensive legal costs responding to 21 different law suits in 18 states, spearheaded mostly by the Democratic Party, to challenge Nader's right to appear on the ballot.<p>
In Seattle, Nader admitted it took him 20 years, but he said that he is now convinced that the Democratic Party cannot be reformed. It is, he said, hopelessly committed to the interests of the rich.<p>
In San Francisco, Nader spoke of the illegal nature of the war in Iraq perpetrated by the White House with the complicity of the Democratic Party. Nader implored the audience to stand up against the war, asking, "How are you going to ask the next 100,000 Iraqis to die for a mistake--which is more than a mistake, a crime?"<p>
<i>Steve Leigh, Justin Powers and Keith Rosenthal contributed to this report.</i> <p>
