<font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Resisting soldiers need our support</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 4, 2005 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">THERE'S NOTHING like a strong dose of reality to put things in perspective.<p>
No one--in this country at least--is more familiar with the reality of war than those who have been forced to risk their own lives at the Pentagon's behest, to carry out its war crimes abroad. Military resisters are showing their potential as a powerful component of the antiwar movement--forcing antiwar discourse to return to earth within the U.S. Empire.<p>
This was certainly the case when members of Iraq Veterans Against the War (IVAW) and Military Families Speak Out (MFSO) spoke to a crowd of more than 400 at Boston's Faneuil Hall on Sunday, January 30--the day that the Iraqi elections were held.<p>
The election itself was mentioned only in passing--befitting the role of "democracy" in Iraq under U.S. occupation, where U.S. troops are formally committed through the end of 2006. "We've all seen banner days come and go," said MFSO founder Nancy Lessin, of the election. "We're seeing it all again, the quagmire, the war that never should have started."<p>
IVAW founder Mike Hoffman provided an instant reality check for those who stress the U.S.'s responsibility to stay and help "rebuild" Iraq: "To those who say we can't cut and run, let's be clear. The military can't fix the problem. We <i>are</i> the problem."<p>
In May, former Marine Staff Sgt. Jimmy Massey, a founder of IVAW, told the <i>Sacramento Bee</i> why he turned against the war--and began to understand the Iraqi resistance. When asked by reporters, "What does the public need to know about your experiences as a Marine?" Massey answered, "The cause of the Iraqi revolt against the American occupation. What they need to know is we killed a lot of innocent people."<p>
Massey, a 12-year veteran of the Marines, was not predisposed to sympathize with the insurgents. But his experience at a military checkpoint turned him against the war.<p>
"There was this one particular incident...It involved a car with Iraqi civilians," he described. "We fired some warning shots. They didn't slow down. So we lit them up...and one gentleman looked up at me and said: 'Why did you kill my brother?' [My commanding officer] came up to me and says: 'Are you OK?' I said: 'No, today is not a good day. We killed a bunch of civilians.' He goes: 'No, today was a good day.' And when he said that, I said, 'Oh, my goodness, what the hell am I into?'"<p>
And the U.S. government, after forcing U.S. troops to commit war crimes, turns its back on the war wounded when they return, emotionally scarred for life.<p>
"I am a little sister who lost her big brother," Debra Lucey--whose brother, 23-year-old Marine reservist Jeffrey Lucey, hanged himself in June--told the crowd at Faneuil Hall.<p>
Lucey's family sought help from the Veterans Administration (VA), to no avail. "[Jeffrey's] body died at home, but his soul died in Iraq," Debra said.<p>
A U.S. military statement responded coldly to the growing number of military resisters applying for conscientious objector (C.O.) status: "Soldiers who join the U.S. army volunteer and know the obligations of their service and their personal commitment to their fellow soldiers."<p>
Yet as military resister Camilo Mejia argued: "I believe most young people who join the military do so for reasons other than war...I was also seduced by the promise of a free college education, which later turned out to be not so free."<p>
On Friday, May 21, 2004, Mejia--who was denied conscientious objector status after serving in Iraq--was sentenced to one year in prison. That was just days before Jeremy Sivits, the first U.S. soldier to face court martial in the Abu Ghraib prison scandal, received the same sentence--for torturing Iraqi prisoners.<p>
Military resisters, facing prison time, are motivated by much more than self-interest. Navy resister Pablo Paredes said in a recent interview, "I know other people are feeling the same way I am, and I'm hoping more people will stand up. They can't throw us all in jail."<p>

