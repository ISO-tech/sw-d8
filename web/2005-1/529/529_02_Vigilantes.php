<font face="Times New Roman, Times, serif" size="5"><b>Vigilantes prepare to "patrol" border</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jeff Bale</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 4, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">SOME 200 vigilantes are expected to descend on the Arizona border with Mexico later this year. As part of the "Minuteman Project," these self-described "patriots" intend to camp out for the entire month and "patrol" the U.S. border for undocumented immigrants.<p>
With the U.S. Border Patrol cracking down in Southern California and Texas, the treacherous Sonoran Desert on the Arizona-Mexico border has become the point of entry of last result for undocumented immigrants. Because of the rough terrain and intense heat, and the gangs that exploit the vulnerability of these immigrants, hundreds die each year on the trek.<p>
But racist vigilante violence has been on the rise as well. The most notorious incident dates back to 1976, when rancher George Hanigan and his two sons hogtied and tortured a group of Mexican men by burning the soles of their feet and shooting them in the back with buckshot. The Hanigan sons were acquitted (George Hanigan died before the trial began).<p>
As immigration has shifted to Arizona, so, too, have the activities of vigilante groups like the Minuteman Project. Minuteman Project founder James Gilchrist--who lives in California, not Arizona--insists that volunteers who participate are not to engage immigrants directly. <p>
Yet the "affirmation" on the organization's Web site volunteers are asked to agree to is titled "April 1, 2005: D-Day"--and comes with the ominous whistling theme song from <i>The Good, the Bad and The Ugly</i>, the one that plays before anyone gets killed in the movie. Gilchrist sums up his restrictions on volunteers with this list: "No rifles. No long guns. Side arms are okay. No camouflage when in town. And Swastikas are not welcome."<p>
But the Southern Poverty Law Center, which tracks neo-Nazi and other hate groups, says that this is a lie. The civil rights group has focused specifically on the Minuteman Project because it has gotten so much traffic on neo-Nazi Web sites.<p>
The increase in vigilante violence follows Arizona's passage of a harsh anti-immigration proposition last November that requires immigrants to prove their U.S. citizenship before voting or receiving state services, and that punishes state workers for not enforcing these requirements.<p>
Kathy McGee, one of the main proponents of the anti-immigrant referendum, recently said that people "in every state except Hawaii" have contacted her group to find out how to pass similar measures in their state. No wonder groups like the Minuteman Project feel more confident.<p>
The radical right is focusing on the immigration issue. Now is the time for pro-immigrant forces to renew an activist movement that defends immigrants and beats back racism.<p>

