<font face="Times New Roman, Times, serif" size="5"><b>New setback for military tribunals</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 4, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE BUSH administration is doing everything in its power to keep detainees at its prison camp in Guant&aacute;namo Bay, Cuba, in a legal limbo. But this week, federal Judge Joyce Hens Green ruled that under a U.S. Supreme Court decision last June, detainees have the right to challenge the basis of their detention in U.S. courts--and that the Bush administration's procedure to determine detainees' status as "enemy combatants" is unconstitutional.<p>
Green concluded that the Combatant Status Review Tribunals--created nine days after the June Supreme Court ruling to decide which detainees should be labeled "enemy combatants," thus making them ineligible to appeal their cases in U.S. courts--are inherently unfair.<p>
Under the rules for the tribunals, detainees were left to argue their cases for themselves, without assistance from lawyers--and were kept from seeing the evidence the government is using to label them as terrorists. <p>
In November, when another court ruling forced the Pentagon to suspend the tribunals, of the 104 detainees already ruled on, 103 had been found to have been "properly" deemed unlawful enemy combatants.<p>
Green also pointed out in her opinion that U.S. officials haven't said how they will determine when the "war on terrorism" has ended. "The government has conceded that the war could last several generations, thereby making it possible, if not likely, that 'enemy combatants' will be subject to terms of life imprisonment at Guant&aacute;namo Bay," Green wrote.<p>
Michael Ratner, president of the Center for Constitutional Rights, which filed the suit against the tribunals, said Green's decision was a victory. The ruling, he said, "reaffirms that the Guant&aacute;namo detainees cannot be imprisoned outside the law, that they have a constitutional right to a fair hearing and that evidence resulting from torture and coercion cannot be used to continue their imprisonments."<p>

