<p><font face="Times New Roman, Times, serif" size="4"><b>Radio Chomsky goes online</b></font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
For a few years now, I have been operating a nonprofit 24-hour radio station broadcasting diverse world-affairs commentary, sourced from all over the planet. Radio Chomsky (<a href="http://www.radiochomsky.com" target="_blank">www.radiochomsky.com</a>) is now in a position to stream live audio via the Internet to listeners around the world. <p>
Almost every interview, conversation, speech, discussion and rant we broadcast was downloaded from the Internet, mostly from <a href="http://www.radio4all.net" target="_blank">www.radio4all.net</a>. As a non-profit station, we are permitted to rebroadcast these programs.<p>
It is my personal belief that information like this should be much more accessible to those people who are interested. I am also continuously interested in additional sources of audio material and information like this for broadcast. Any help that you are able to provide will be greatly appreciated.<br>
<b>Aaron Skudder, </b>Auckland, New Zealand<p>

