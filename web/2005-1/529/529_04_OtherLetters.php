<font face="Times New Roman, Times, serif" size="4"><b>Victim of a backdoor draft</b></font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
I was glad to see your coverage this week on the backdoor draft being used by the military through stop-loss orders ("Feeling a draft?" January 21). Case in point: Emiliano Santiago, who just finished serving eight years with the Oregon National Guard. Outrageously, the military then extended his termination date to 2031. <p>
I was in Portland in December when Santiago went to court for a preliminary injunction that would have allowed him to stay in the U.S. long enough to appeal the order. However, the judge denied the injunction, and he will now be sent to Afghanistan on February 2. <p>
An article in the <i>Oregonian </i>made it clear that there was no real legal basis for the judge's decision. "[U.S. District Judge] Panner said he thought the military would endure more harm than Santiago if he ruled in the soldier's favor, essentially agreeing with the government's argument that thousands of military members who are subject to stop-loss orders might file similar legal challenges," the paper reported.<p>
This case clearly exposes whose side the legal system in this country is on, as well as the disgusting length the military is willing to go to fight their unjust wars. However, it also exposes their fear of the growing number of soldiers who are speaking out and refusing to serve. <p>
While there were several activists and sympathizers present at the court hearing, a larger and better-organized movement might have pressured the judge into a different decision. It is up to us to organize and give confidence to the soldiers who are resisting and demand that we bring the troops home now. <br>
<b>Leela Yellesetty, </b>New Haven, Conn.<p>
