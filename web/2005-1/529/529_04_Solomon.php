<font face="Times New Roman, Times, serif" size="5"><b><i>SW </i>twisted my argument</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 4, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
"Was Solomon right to warn that Bush is, essentially, ushering in a fascist dictatorship?" That's a question that an essay in <i>Socialist Worker </i>asked ("Are we facing the threat of fascism," January 21). But the rhetorical question is based on a premise that indicates a lack of reading comprehension.<p>
The <i>Socialist Worker </i>article claims that I warned that Bush "is"--"essentially"--"ushering in a fascist dictatorship." But that's a very odd way to summarize what I actually wrote.<p>
Here are my words that the article quotes: "We should not gloss over the reality that the Bush team has neared some elements of fascism in its day-to-day operations--and forces inside the Bush administration would be well-positioned to move it even farther to the right after 2004." And: "We don't want to find out how fascistic a second term of George W. Bush's presidency could become."<p>
Socialists and other progressives should strive to be accurate rather than slipshod, precise rather than hazy. And we should try to be dialectical rather than turning some arguments into straw.<p>
I wonder whether <i>Socialist Worker </i>is taking a position that the Bush team has <i>not</i> neared any "elements of fascism" in its day-to-day operations. Or that forces inside the Bush administration are <i>not</i> "well-positioned to move it even farther to the right." Or that we <i>did</i> "want to find out how fascistic a second term of George W. Bush's presidency could become."<br>
<b>Norman Solomon, </b>San Francisco<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b><i>Socialist Worker </i>replies:</b> <br>
Norman Solomon is right that the one sentence he quotes from a recent <i>SW </i>article mischaracterizes his analysis of the Bush administration and its "fascistic" tendencies.<p>
That said, what does he have to say about the substance of that article? Its point wasn't to challenge the position we wrongly attributed to him (that Bush is actually ushering in a fascist dictatorship), but to show how misusing the term fascism by applying it loosely--to anything smacking of government repression and violence--sows political confusion.<p>
We argued that what Solomon calls the "elements of fascism in [the Bush administration's] day-to-day operations" can be found in governments that are considered models of parliamentary democracy--including the U.S. government, under both Republicans <i>and </i>Democrats. Does he disagree?<p>
We did point out that Solomon's argument wasn't about the technical definition of fascism, but was "mainly an effort to convince progressives considering a vote for Ralph Nader that they had no choice but to back Democrat John Kerry."<p>
<i>Socialist Worker </i>argued during the presidential campaign that progressives can't effectively challenge a right-wing assault by supporting a Democrat like Kerry who offers a slightly less extreme version of the same assault--a candidate whose "opposition" to Bush was limited to bickering over the details of the "war on terror" or the USA PATRIOT Act, instead of challenging the substance of them.<p>
Solomon, by contrast, referred to Bush's "elements of fascism" in a way that exaggerated the differences between Bush and Kerry. His aim, we believe, was to scare progressives into shutting up about their criticisms of the Democrats and getting behind Kerry.<p>
The left has paid a heavy price for the ABB strategy that Solomon and others promoted--for one thing, the virtual disappearance of the antiwar movement for the last year, outside of the demonstration at the Republican National Convention. Norman should take up the debate about these real questions--rather than twist <i>our </i>words to suggest that we in any way welcome George Bush in the White House.<p>

