<font face="Times New Roman, Times, serif" size="4">Bush asks for $80 billion more to fund occupation</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Next phase in the U.S. war</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 4, 2005 | Page 1</font><p>

<font face="Times New Roman, Times, serif" size="3">THE U.S. media's wall-to-wall coverage of the elections in Iraq featured scenes of voters going to the polls interspersed with sound bites from George W. Bush and Condoleezza Rice. "The world is hearing the voice of freedom from the center of the Middle East," proclaimed Bush.<p>
But the U.S. hasn't brought freedom to Iraq. From the 1991 Gulf War, through a decade of devastating economic sanctions, to Operation Shock and Awe and two years of occupation, the U.S. has reduced Iraq from one of the most advanced societies in the Middle East to one of the poorest countries in the world.<p>
Iraq today is without regular water and electricity supplies. Nearly two years after U.S. forces conquered the capital of Baghdad, residents continue to say that conditions were better under the former regime of Saddam Hussein than the American military. And in a country that sits on the world's second-largest reserves of oil, its people sit for hours and even days in line to buy gas for their cars.<p>
"When I go to vote, my clothes will be filthy, my body will stink, and I will be thirsty," Jabbar Abu Eid, an elderly resident of Baghdad, told the <i>Financial Times. </i>"Freedom means no water, no electricity, no fuel--just elections."<p>
Nor does "freedom" or "democracy" figure into the Bush administration's future plans for Iraq. Bush showed this last week in advance of his State of the Union address when he asked Congress for $80 billion more to spend on the occupation of Iraq--and that's on top of $150 billion he's already spent.<p>
That means more U.S. troops deployed in what a growing number of military analysts call an "unwinnable war." That means more U.S. casualties, like the 31 soldiers who died in a helicopter crash last week--making January 26 the deadliest day yet for American forces. And that means many more Iraqi victims of U.S. violence--which has caused an average of more than 140 Iraqi deaths <i>every day </i>since the invasion began in March 2003, according to the British medical journal <i>The Lancet.</i><p>
The crisis of the U.S. occupation has grown so severe that cracks are developing at the top of the U.S. political establishment. In mid-January, Zbigniew Brzezinski, Brent Scowcroft and James Baker--all high-profile foreign policy experts, the last two veteran Republican leaders--aimed a string of criticisms at the Bush administration that amounted to a call to deploy 500,000 more troops, or withdraw as soon as possible.<p>
Last week, Sen. Ted Kennedy, leading the way for other Democrats, joined in. "We have reached the point that a prolonged military presence in Iraq is no longer productive for either Iraq or the U.S.," Kennedy said.<p>
But Kennedy was careful not to step outside the limits of the debate in official Washington--calling for a phased withdrawal by some time in 2006, and claiming, like the Democrats' presidential candidate John Kerry, that the war in Iraq had detracted from the real "war on terror."<p>
Kennedy and the Democrats can't be trusted to push for an end to this war. The antiwar movement needs to go beyond Washington's fake debate--and support the demands of Iraqis who want to liberate their country from U.S. occupation.<p>
"You cannot give us 'democracy' just like this," one man told antiwar journalist Robert Fisk. "That is one of your Western foreign dreams. Before, we had Saddam, and he was a cruel man, and he treated us cruelly. But what will happen after this election is that you will give us lots of little Saddams."<p>
That's why we must demand that the U.S. get out of Iraq now--and allow Iraqis to determine their own future.<p>

