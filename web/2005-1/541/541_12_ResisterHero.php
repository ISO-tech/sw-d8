<font face="Times New Roman, Times, serif" size="4">Pablo Paredes faces desertion charge</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A hero for resisting</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 29, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">FERNANDO SUAREZ DEL SOLAR's son Jes&uacute;s was one of the first U.S. soldiers killed during the March 20003 invasion of Iraq. Since then, Fernando has spoken out tirelessly against the war. He sent the following essay about military resister Pablo Paredes to <i>Socialist Worker.</i> <p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">PABLO PAREDES is a true hero and a victim of a poorly named military justice system that will charge him with desertion and bad conduct. It is ridiculous and immoral that some soldiers who mistreated and abused Iraqi prisoners have been set free, while Pablo is punished for refusing to participate in the violation of the basic rights of another human being.<p>
Pablo Paredes is a clear example that this war ignores international law and breaks with international conventions signed by the U.S. government. Without any scruples, Bush invaded and occupied Iraq for his own selfish reasons. Pablo refused to be an accomplice to that illegal act, and he refuses to transport decent young Marines and sailors to die or to kill innocent people. <p>
Pablo defended life, and so he is brought before a court-martial. Because he defends the lives of young Marines, publicly states his opposition to the war, and speaks as a conscientious objector, Pablo is accused.  <p>
But his crime is not what the military says it is. His "crime" is that he speaks from a moral position that Bush and his lackeys in the Pentagon do not understand. For speaking out, he may be tried and sent to prison as a warning to other young servicemen and women that they must remain silent.<p>
Pablo Paredes, Camilo Mejia and many others have followed their conscience and spoken out for human rights. They are heroes and should serve as models for thousands of U.S. military personnel who may want to refuse orders that violate the most basic human rights. <p>
In 1973, U.S. troops withdrew from South Vietnam after a war that cost more than 58,000 U.S. lives and millions of innocent Vietnamese lives. Will we have to wait until we reach these numbers again before our children are brought home from Iraq? Will we have to witness more young people destroyed mentally and physically? Will we have to wait until more families like mine directly suffer the consequences of Bush's crimes? <p>
My son Jesus and more than 1,500 others have died because of Bush's hypocrisy and greed. Pablo, Camilo and others have been saved, but will others save themselves? Will we have to sit waiting and weeping while we endure this administration's lies?  <p>
No. We can wait no longer. Today is the time to act--to support young men like Pablo and Camilo and to demand that our troops come home now.<br>
<b>Fernando Suarez del Solar, </b>San Diego<p>

