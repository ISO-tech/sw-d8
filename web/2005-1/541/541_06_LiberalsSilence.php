<font face="Times New Roman, Times, serif" size="5"><b>Liberals' deafening silence</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 29, 2005 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3">THOSE "FUNDAMENTALIST extremists" are at it again, trying to force women back to the Dark Ages. Christian fundamentalists, that is.<p>
Intoxicated by their recent success at the voting booth, Christian fundamentalists are no longer satisfied with pressing their claim that fetal rights supercede those of a living, breathing woman. They are now championing the rights of the "unborn" zygote, a fertilized egg, destroyed by oral contraceptives in the moments after conception.<p>
In a battle now raging in at least 23 states, the Christian Right has expanded its crusade against abortion to include these "killer" birth control pills. Pharmacy by pharmacy, members of "Pharmacists for Life" are refusing to fill doctors' prescriptions for emergency "morning after" pills and other oral contraceptives. <p>
In addition, these pharmacists of conscience refuse to refer patients to other pharmacists to perform the deadly deed. Karen Bauer, the group's president, stated plainly, "A pharmacy should be for healing. It should not be for killing."<p>
Meanwhile, back in Washington, Bush is doing his part to further advance anti-abortion mythology, announcing on April 22 his administration's intent to aggressively enforce "The Born-Alive Infant Protection Act of 2002," requiring doctors to keep a fetus alive that survives an abortion.<p>
Bush administration officials admitted they don't know how often a fetus survives an abortion and noted no complaints about a lack of enforcement. This could be because there aren't any. No medical advancement can alter the laws of anatomical development, which preclude fetal survival outside the womb before hearts and lungs develop around the twentieth week of pregnancy.<p>
The overwhelming majority of abortions after this point are performed only to save the health or life of the woman. In fact, only four one-hundredths of 1 percent of legal abortions are performed after the second trimester. And more than 95 percent of all abortions are performed during the first 15 weeks of pregnancy.<p>
With ever more outlandish discourse, the Christian Right has gained the upper hand in the battle over abortion in Bush's second term. But the pro-choice movement has helped pave the way.<p>
Just one year ago, a million pro-choice supporters gathered for the March for Women's Lives in Washington, D.C. Surely, the human material exists to form a "Pharmacists for Choice" group, to picket the local pharmacy that refuses to dispense birth control pills and to assert the rights of the three million women who face unplanned pregnancies in the U.S. each year. Yet the pro-choice movement is ceding ideological ground as fast as the Christian Right demands it.<p>
NARAL Pro-Choice America, the nation's largest pro-choice organization, offered "no comment" in response to Bush's absurd announcement on "born-alive" fetuses. Its silence is deafening.<p>
The pro-choice movement has reached a crisis point, stemming directly from its uncritical support for the Democratic Party. Having featured Hillary Clinton as an "honored guest" at last year's March for Women's Lives, the women's movement is paralyzed now that Clinton is leading the party's full retreat on abortion rights.<p>
In January, Clinton called abortion a "sad, even tragic choice" in an overture to the religious right--while asking "people of good faith to find common ground in this debate." <p>
Feminist Naomi Wolf, a political consultant for both Al Gore and Bill Clinton, has long stumped for the other side on key aspects of the abortion debate. In a 1997<i> New York Times</i> editorial, Wolf called on pro-choice supporters to join abortion opponents to lower the nation's "shamefully high abortion rate."<p>
"The pro-choice movement should give God a seat at the table," urged Wolf, lambasting the pro-choice movement for framing its defense of abortion rights around "a woman's right to choose," which she claimed is "abstract." Perhaps not surprisingly, Wolf is now proposing a ban on abortion after the first trimester, as <i>Nation </i>columnist Katha Pollitt noted with dismay in the May 2 issue.<p>
Mainstream feminists and their organizations are doing more than shifting gears in the struggle for abortion rights. They are following the Democratic Party in abandoning it, at the very moment when the anti-abortion crusade is moving ahead at full throttle.<p>

