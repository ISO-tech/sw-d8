<font face="Times New Roman, Times, serif" size="4"><b>Slime-mold George</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WAS THAT George Bush who just scurried under your sink?<p>
Two former Cornell University entomologists, who recently had the job of naming 65 newly discovered species of the slime-mold beetles, named three species after Bush, Dick Cheney and Donald Rumsfeld--dubbing them: Agathidium bushi, Agathidium cheneyi and Agathidium rumsfeldi.<p>
Naming the beetles after Bush, Cheney and Rumsfeld had nothing to do with their physical features, but was intended to pay homage to them, said former Cornell University professor Quentin Wheeler. "We admire these leaders as fellow citizens who have the courage of their convictions and are willing to do the very difficult and unpopular work of living up to principles of freedom and democracy rather than accepting the expedient or popular," said Wheeler.<p>
Incidentally, slime-mold beetles are also named for the fungi-like molds on which they feed. For anyone looking to hunt down one of the new beetles, Agathidium bushi has so far been found in southern Ohio, North Carolina and Virginia. Rumsfeldi and cheneyi are from Mexico.<br>
-- <b>Associated Press, </b>April 13, 2005<p>

<font face="Times New Roman, Times, serif" size="4"><b>Conservative smackdown</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THE WRESTLER formerly known as "The Ultimate Warrior" (now apparently just "Warrior") made a professional-wrestling-sized ass of himself at the University of Connecticut.<p>
The former World Wrestling Federation star, a hard-line right winger, was invited to speak by the College Republicans. But "Warrior" was a little nutty even for the College Republicans, "screaming and stomping on the stage," according to liveaudiowrestling.com, and provoking the crowd into a frenzy. The evening went south when Warrior answered an Iranian student's question by telling him that he needed to "get a towel"--and pontificated on such subjects as why "Queering don't make the world work." <p>
The UConn College Republicans issued an apologetic press release the next day, noting that they were sending individual letters of apology to, "the Rainbow Center, AQUA, QUAD, the African American Cultural Center, the Asian American Cultural Center, the Puerto Rican/Latin American Cultural Center, the Iranian Students Association and the Women's Center." And, they added, "this is not a complete list of the groups that will receive a direct written apology from the College Republicans." <p>
But the press release provoked an outburst "From the Desk Of Warrior"--who, speaking in the third person, announced that "it is somewhat sad to see how utterly spineless the UConn College Republicans have turned out to be. Not a single UConn CR voiced any objection to Warrior after the event. <p>
"The detailed e-mails between Warrior and the UConn CRs reveal that the CRs repeatedly encouraged Warrior to single out the Tent City Trash for some reeducation. Yet it now seems that the CRs have collectively decided to bow down and beg forgiveness from various extremist, anti-American, left-wing groups who infest the UConn campus. Perhaps the UConn CRs should refrain from engaging in political activism until such time as they develop enough backbone to be able to withstand not being liked by their opponents." <br>
-- <b>DemocraticUndergound.com, </b>April 11, 2005<p>

