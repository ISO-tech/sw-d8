<font face="Times New Roman, Times, serif" size="5"><b>Protests drive out Ecuador's president</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 29, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">ALONSO ZHINGRE is a socialist and social movement activist in Ecuador. He wrote this account for <i>Socialist Worker </i>and <i>Obrero Socialista </i>of the mass mobilizations in the capital of Quito, which toppled the government of Lucio Guti&eacute;rrez.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">THE SOCIAL crisis caused by the implementation of the neoliberal agenda by the government of President Lucio Guti&eacute;rrez spurred three weeks of struggle this month, culminating in a mass mobilization that brought down the government on April 20.<p>
The movement was spontaneous and impressive, representing the radicalization of different social and popular sectors against the regime. The night before Guti&eacute;rrez was forced out, no less than 200,000 people marched on the government palace. They were brutally repressed by the police and military.<p>
The U.S. embassy has had much to do with this crisis, because Guti&eacute;rrez was a strong ally in bringing about a free-trade agreement and Plan Colombia, a strategy for militarizing the Andes region in the name of the "war on drugs," for which the U.S. base in Manta was being opened. All of this is left up in the air now with Guti&eacute;rrez gone.<p>
The movement took the name "Forajidos" ("outlaws"), adopting the insult that the government used to describe mobilizations of the social movements.<p>
These struggles have had some distinctive features, one of the most outstanding being the direct participation of the media, like Radio La Luna. Radio La Luna helped to call, centralize and direct the mobilizations. Its microphones became tools for direct participation. <p>
At this time, the people are demanding "que se vayan todos" ("throw them all out")--and the immediate creation of popular assemblies to watch over and control a new democracy. What is frustrating is that there is no political leadership that can channel the peoples' anger.<p>
The movement is demanding that new president, Alfredo Palacio, immediately call a referendum so that the people can decide on a free trade agreement and the Plan Colombia bases in Manta. It is also calling for the immediate disbanding of Congress, to make way for elections in less than two months.<p>
Overall, the demonstrations showed the highest expression of social struggle--and the real possibilities for building a movement against capitalism and for international socialism.<p>

