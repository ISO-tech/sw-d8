<font face="Times New Roman, Times, serif" size="4">Walkouts at Columbia and Yale over fight for union rights</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Grad strikes hit campus</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jonah Birch and Mitch Day</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 29, 2005 | Page 15</font> <p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Hundreds of workers rallied at Columbia University and New York University (NYU) on April 21 in solidarity with striking graduate employees at Columbia and Yale. Eight buses filled with Yale union members traveled from New Haven, Conn., for the rally at Columbia, which was addressed by AFL-CIO President John Sweeney and UNITE HERE leader John Wilhelm.<p>
Graduate student employees at both universities organized weeklong strikes meant to galvanize support for their struggle for union bargaining rights.<p>
Administrators at the two schools have consistently opposed the graduate students' right to organize, insisting that they aren't workers. The National Labor Relations Board (NLRB), dominated by right-wingers, supported the administrations' position in a ruling last year that prevented Columbia grad students from joining a collective bargaining unit.<p>
Columbia and Yale, however, rely on graduate students to do much of the teaching at their schools. And maintenance, clerical and food service workers at both universities recognize that graduate students are in fact workers, as evidenced by the high level of solidarity exhibited at the rally.<p>
Graduate student unionization represents an important struggle for labor right now. Administrators at schools like Columbia and Yale aren't going to back down unless they're forced to by campus labor. Already, Columbia has ignored the results of a pro-union check-off campaign among graduate students there, even though New York Attorney General Eliot Spitzer certified the results of the vote.<p>
Cross-campus solidarity is key to building a movement that can win graduate students the right to unionize. As one member of Yale's Graduate Employees and Students Organization pointed out, "For this to happen anywhere, it has to happen everywhere."<p>
Downtown, NYU's Graduate Students Organizing Committee (GSOC), affiliated with United Auto Workers Local 2110, held another rally in solidarity with the strike and to press their own demand for union recognition. Some 300 people joined the rally and moving picket, including striking graduates from Columbia and, from NYU, undergraduates and members of unions representing clerical and technical workers and adjunct professors.<p>
NYU has stated it will not make an official decision on bargaining again with GSOC until mid-summer (conveniently when many graduate students are away from campus). While majority support exists for the 1,200-member union, it will take consistent organizing to translate this into active defense as NYU continues its attacks.<p>


