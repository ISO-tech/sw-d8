<font face="Times New Roman, Times, serif" size="5"><b>California students protest cuts in education</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Brian Cruz</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 29, 2005 | Page 15</font> <p>

<font face="Times New Roman, Times, serif" size="3">COLLEGE STUDENTS marched and rallied across California April 20 to protest cuts in higher education. Fifteen schools participated, with 800 turning out in Los Angeles, 400 in San Francisco, and 150 in Santa Cruz. More than 400 students from at least five San Diego campuses marched downtown.<p>
Antiwar and counter-recruitment messages dominated the chants, including, "Get the military out of our schools" and "Teachers not recruiters." In several cities, students were joined by not only faculty and staff but also elementary and high school students, parents and teachers. <p>
This first statewide action of Action in Defense of Education (AIDE) demanded that Proposition 98--a state law guaranteeing funding to California schools--be fully funded, that the richest 1 percent of Californians be taxed, and that no more cuts be made to education. <p>
At both the University of California (UC) and California State University (CSU) systems, student fees have increased 60 percent during the past decade. "More and more, UC is becoming an institution for the privileged and that needs to change," said UC-San Diego student Maki Matsumura.<p>
Fees at California community colleges, once free, have more than doubled from $11 to $26 per unit--causing these colleges to lose 400,000 students overall. The fee hikes are part of a recent pact between the CSU and UC schools and Gov. Schwarzenegger. <p>
City College of San Francisco economics student Jerald Reodica, however, spoke to a walkout of 400 about the real priorities of the state. "Arnold claims that California is in a financial crisis," said Reodica. "His solution would be to privatize public pensions, limit disability claims, and cut education. What he wants to spend money on instead is prisons."<p>
Over the past four years, Sacramento has reduced school funding by $9.8 billion, despite Proposition 98, passed by voters in 1988. Meanwhile, California is planning to spend $220 million on a new facility to house death row prisoners. On the heels of education protests that brought thousands of protesters to Sacramento in previous years, these demonstrations expose the continuing anger and willingness to fight back of students in California. <p>
<i>Vera Lux contributed to this report</i>.

