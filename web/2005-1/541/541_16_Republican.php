<font face="Times New Roman, Times, serif" size="4">Republicans on the rampage</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>What will it take to stop them?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 29, 2005 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">GEORGE BUSH and the Republicans think they can get away with anything. From the gifts from lobbyists amassed by House Majority Leader Tom DeLay (R-Texas), to the nomination of superhawk John Bolton to the post of ambassador to the United Nations (UN), to congressional Republicans' determination to fill the courts with arch-conservatives, Bush and his allies are oozing confidence.<p>
Last weekend, a taped message from Senate Majority Leader Bill Frist (R-Tenn.) was a highlight of "Justice Sunday: Stop the Filibuster Against People of Faith'' telecast sponsored by the Family Research Council.<p>
The religious right organized the event to oppose Senate Democrats' threats to use a filibuster--in which one senator holds the floor indefinitely, holding up all business--to block the nomination of right-wing judges to the federal courts. Frist says that he is willing to rewrite the Senate rules to bar them from being used in the case of judicial nominations--a maneuver that is being called the "nuclear option" because it goes against 200 years of Senate proceedings.<p>
The Family Research Council claimed in a flyer that the filibuster tactic "was once abused to protect racial bias, and now it is being used against people of faith." But the filibuster is most associated with attempts at blocking civil rights legislation--including by Republican Strom Thurmond in 1957 and Democrat Robert Byrd in 1964. And this complaint about bigotry is coming from the organization behind recent legislative attacks on gays and lesbians' equal right to marry.<p>
Another speaker at the rally, James Dobson, founder of the Christian group Focus on the Family, railed about how the Supreme Court was "the despotism of the oligarchy." But Dobson sees eye to eye with the judges that Bush has nominated for the federal bench.<p>
For example, as a Texas Supreme Court justice, Priscilla Richman Owen was best known for her hard-line opposition to abortion, especially for minors. Owen's nomination was blocked by congressional Democrats in Bush's first term, along with that of Janice Rogers Brown, a California Supreme Court judge. In the past, Brown has said that seniors on Social Security "blithely cannibalize their grandchildren" and has called the New Deal a "socialist revolution."<p>
Also on the Bush administration's wish list is the nomination of John Bolton to be ambassador to the UN.<p>
Bolton is an odd choice, since it's unclear whether he thinks the UN should exist. A veteran hawk who served in the Reagan and Bush Sr. administrations, Bolton has in the past favored suspending U.S. dues payments to the UN. In a 1994 speech before the World Federalist Association, he declared that if the UN headquarters in New York "lost 10 stories, it wouldn't make a bit of difference."<p>
The Bush administration wants to guarantee that the UN continues to do its bidding--or else. And Bolton is the kind of man who can get the job done.<p>
It's obvious that there's a war going on in Washington. Unfortunately, only one side is fighting.<p>
So far, congressional Democrats have done little to stand up to the Republican agenda. Many defected to support the credit card industry's dream of bankruptcy "reform," and all but a few dozen supported the bill to spend $81 billion more on the occupation of Iraq and Afghanistan. Last week, only two Democratic senators voted against the nomination of John Negroponte--who helped orchestrate Washington's secret wars in Central America in the 1980s--to be national intelligence director.<p>
And when it comes to the Republicans' plan to use the "nuclear option" against a Democratic filibuster, the party's leader in the Senate, Harry Reid, pointed out "that Congress has already approved 205 of this administration's nominees. Apparently, a 95 percent confirmation rate is not enough for this president." Sen. Joseph Biden (D-Del.), a senior member of the Judiciary Committee, told ABC News' <i>This Week:</i> "I think we should compromise and say to them that we're willing to--of the seven judges--we'll let a number of them go through, the two most extreme not go through, and put off this [rule-change] vote."<p>
If we're going to see any real opposition to the anti-choice, pro-war bigots in the courts, Congress and the White House, we're going to have to build it from the bottom up. We have to organize to tell Republicans and Democrats alike that we aren't going to compromise with our rights.<p>


