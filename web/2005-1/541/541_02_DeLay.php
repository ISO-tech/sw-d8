<font face="Times New Roman, Times, serif" size="4">DeLay blames scandal on "liberal media"</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Is The Hammer finished?</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 29, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">"THE TIME has come that the American people know exactly what their representatives are doing here in Washington. Are they feeding at the public trough, taking lobbyist-paid vacations, getting wined and dined by special-interest groups? Or are they working hard to represent their constituents?"<p>
When "The Hammer"--a.k.a., House Majority Leader Tom DeLay (R-Texas)--delivered that scathing speech in the House of Representatives in November 1995, he didn't know that a decade later, he would be fighting for his political career over allegations of ethics violations.<p>
DeLay is up to his neck in scandal today after revelations that his political action and campaign committees paid his wife and daughter more than $500,000 since 2001, and that DeLay himself accepted lavish vacations, courtesy of paid lobbyists.<p>
One of his trips--a vacation to Britain that included lodging at London's Four Seasons and an outing at Scotland's famous St. Andrews golf club--was paid for by a nonprofit center for "public policy research," which in turn was funded in part by money that lobbyist Jack Abramoff scammed from six Native American tribes, in exchange for bolstering their casino interests. According to recent reports, DeLay's airfare to London and Scotland was charged straight to Abramoff's American Express credit card.<p>
At the time of the trip, Congress was considering legislation that would have shut down Internet gambling--and jeopardized the livelihoods of some of Abramoff's biggest clients. The proposal died a few weeks after DeLay's return.<p>
Abramoff--whom DeLay once called "one of my closest and dearest friends"--is now under criminal investigation by the FBI, Interior Department, IRS and the U.S. Senate.<p>
Some prominent Republicans have distanced themselves from DeLay, but others--like Sen. Trent Lott, who was ousted as Senate majority leader two years ago after making racist comments at a birthday party for Strom Thurmond--are rising to his defense. Their case is that almost everything that DeLay is being criticized for is legal.<p>
They're right. The real scandal isn't what DeLay did to break the law, but that so much of what has emerged about him is common practice in Washington--among both Republicans and Democrats.<p>
For example, several members of Congress have paid family members on staff. And when it comes to lavish vacations, these are simply one of the perks that comes with the job--as long as lawmakers follow the loopholes. Members of Congress are prohibited from accepting more than $50 at a time in meals, gifts or expenses. But most congressional ethics rules are waived when trips are deemed "educational."<p>
As <i>Wall Street Journal</i> reporter Brody Mullins wrote, "Because the trips are paid for by corporations and trade associations--and not the hired guns who lobby for them--such trips are permitted under House and Senate rules."<p>
According to Mullins' report, "Members of both parties are increasingly taking advantage. In 2004, U.S. corporations and business associations sponsored more than 1,900 trips for lawmakers and their aides, up from 1,400 in 2000. Last year's trips cost more than $3 million, a 50 percent increase since 2000, according to a <i>Wall Street Journal</i> review of all congressional travel records from those two years." <p>
That explains how DeLay and more than 30 other lawmakers and congressional staffers were "educated" with a five-day stay at one of the best resorts in Hawaii in January, courtesy of the American Association of Airport Executives, an industry group that represents U.S. airports and airlines. The trip came just as airport executives were pushing Congress to protect the Airport Improvement Program, a federal fund that has nearly doubled to $3.5 billion over the past five years. <p>

