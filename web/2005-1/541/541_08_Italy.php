<font face="Times New Roman, Times, serif" size="5"><b>Behind the crisis facing Berlusconi</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 29, 2005 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">Italian journalist SERGIO FINARDI looks at the crisis facing Italian Premier Silvio Berlusconi, who dissolved his government this month and formed a new one in the hopes of bolstering flagging approval ratings.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">"ITALY PLUNGES back into political chaos" was the headline of an April 18 dispatch from Italy by Reuters correspondents.<p>
What happened? Riots, armed unrest, military uprising, a remaking of U.S./NATO's "strategy of tension" of the 1970s, with an attempted coup d'etat by the far right? Not at all. <p>
It took only an election for governors and other administrative offices (mayors, county presidents, etc.) in 14 out of Italy's 20 regions and some subsequent contradictory statements by the Italian premier to lead Reuters correspondents to declare Italy on the edge of "political chaos."<p>
The regional vote did reverse the right-wing trend of recent years, possibly anticipating the result of national elections due for 2006. Eight regions previously governed by the center-right coalition (that includes the "former" fascist party National Alliance) passed to Unity, the coalition of center-left parties. Unity won a total of 12 governorships, as well as many majorities in the most important cities.<p>
The poor result for the center-right parties in most of the Italian regions obviously affected the internal stability of the center-right coalition, led since 2001 by Silvio Berlusconi, a media mogul with many unsolved problems with the Italian justice system and a staunch ally of George W. Bush on Iraq.<p>
Some of Berlusconi's allies asked him to resign and reshuffle the government. Berlusconi's opponents and Unity--led by former European Union chief Romano Prodi, who during the 1970s was a moderate in the ruling Christian Democrats, and CEO of the most powerful Italian state-owned industrial complex, IRI--also asked for elections.<p>
Berlusconi apparently agreed to resign, but after a day of frantic political consultations, he reversed his position and declared his willingness to face his opponents in parliament. Two days later, Berlusconi reversed his position again and resigned.<p>
No matter what kind of government he may form to get to the 2006 elections, it is evident that the wind has changed, and Unity has a serious chance to win and form the next government. What does this mean for Italian democracy, the working class, and Italy's position in Europe and its trans-Atlantic relationship?<p>
There are three different answers to these questions. The first and most important is that a center-left coalition will likely halt the assault that Berlusconi and his coalition have waged in the past years on the most progressive parts of the Italian Constitution.<p>
The second answer is that Unity is actually far more "anti-Berlusconi," than "pro-something." Unity's members go from former Christian Democrats to supporters of neoliberal policies (the "Daisy" alliance); and from the Democrats of the Left (part of the former Communist Party, led by Massimo D'Alema, who as Italian premier gave Bill Clinton the full support of Italy for the bombing of Yugoslavia) to Communist Refoundation (which is firmly antiwar and for the defense of the outstanding social benefits and labor laws conquered by Italian workers in the past 50 years). Therefore, what kind of social and international policies that Unity could have once in government remains a mystery.<p>
The third answer is that a center-left government will anyway face the challenge of a stagnant economy, as well as the consequences of the ruinous trends of neoliberal social, labor and fiscal policies. Those policies were surely aggravated by Berlusconi's government, but were actually and firstly promoted by his predecessors--the governments led by Romano Prodi and Massimo D'Alema.<p>

