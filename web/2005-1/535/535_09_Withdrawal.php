<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why we argue U.S. out now</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 18, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">MEDIATE OR delayed withdrawal of U.S. troops in Iraq--this is one of the key questions confronting the antiwar movement today.<p>
The debate isn't entirely new. Tom Hayden, the former Students for a Democratic Society (SDS) activist-turned-Democratic Party liberal, recently wrote on his blog, "The same splits occurred during the Vietnam War between those who demanded 'out now' and those who favored 'negotiations now,' which was roughly a difference between radicals and liberals."<p>
Hayden is right so far. But then he says, "We might remember that in the end these differences mattered little, except for the domestic posture of competing antiwar groups." The differences mattered a lot, though given that Hayden began as more of a radical and ended up as a liberal, it's easy to see why he'd want to blur the distinction.<p>
It was a difference between those who opposed the war and those who were for continuing it, for however long. The radicals who were for "out now" were the most committed, consistent activists who understood that, if you oppose a war (its aims, not just its methods), then you can't be for any part of it.<p>
The liberals who called for "negotiations" were the least consistent, and the most frightened, by mass protests.  Still infused with anticommunist fervor, they were opposed to SDS students organizing their first mass antiwar protest in 1965 and were quick to condemn SDS's policy of "non-exclusion" that explicitly challenged the stuffy anticommunism of the liberal establishment.<p>
They tended not to oppose the war so much as the means by which it was being conducted. They opposed the war's "excesses" but not necessarily the war itself. The leaders pursuing the war always say they are in favor of pulling the troops out--when the time is right. The "right" time, of course, is when the U.S. accomplishes what it aimed to achieve by the invasion in the first place.<p>
What this position concedes, by accepting negotiations, is that the U.S. has established, through its use of military force, its right to negotiate the conditions of its own pullout. To be opposed to immediate withdrawal and for negotiations isn't an antiwar position at all. It's a position of critical support: Please conduct the war differently. Negotiate more quickly, win more quickly and at a smaller cost.<p>
Negotiations Now, formed in 1967, was led by Kennedy liberals like Arthur Schlesinger Jr., who began as a supporter of the war and then became alarmed when no end appeared in sight and protests mounted. At the May 1965 Vietnam Day teach-in in Berkeley, socialist Hal Draper debated liberal peace activist Robert Pickus on this very question.<p>
Pickus argued that he was opposed to the violence committed by the U.S. in Vietnam, but he opposed immediate withdrawal. Draper argued that to call for a pullout of troops "later" is to be for war in the here and now. Draper described Pickus' position as "war now, peace later."<p>
That the position of opposition to immediate withdrawal is not really an antiwar position is even clearer in the current context. The most common argument against the U.S. leaving is that Iraq will fall into chaos and civil war. Ergo, the U.S. must stay and "sort things out."<p>
Liberal Truthout.com commentator William Rivers Pitt argues, for example, "If we haul stakes and leave, we risk having the country collapse permanently into a Balkanized state of civil and religious war that will help to create a terrorist stronghold in the mold of Afghanistan post-1989."<p>
This position places Pitt firmly in favor of occupation. That he justifies his paternalism on the grounds that the invasion was wrong but that the U.S. must now take "responsibility" to fix the problems it created hardly improves his argument.<p>
Should we trust the fox to clean up the hen house after it has broken in and eaten half the chickens? This isn't only a "war now, peace later" position; it accepts the absurd (and terribly paternalistic) idea that the cause of Iraq's horror can also be Iraq's savior!<p>

