<font face="Times New Roman, Times, serif" size="5"><b>Moved On from opposing the war</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 18, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">SO MUCH for the MoveOn.org "revolution." Hailed for their ability to raise big money through small online contributions, those in charge of "online activism" are assimilating into the Democratic Party mainstream--first by backing Howard Dean for Democratic National Committee (DNC) chair and now by abandoning calls for U.S. withdrawal from Iraq.<p>
As media critic Norman Solomon noted, "During the recent bloody months, none of MoveOn's action alerts have addressed what Americans can do to help get the U.S. military out of [Iraq]." Solomon added: "When a large progressive organization takes the easy way and makes peace with war, the abdication of responsibility creates a vacuum."<p>
Actually, Solomon, along with other prominent voices on the antiwar left, also helped create that vacuum by backing John Kerry's prowar presidential campaign last year-- in the name of political realism, of course. Now MoveOn.org is using similar logic--not only to fold its tent on Iraq, but to avoid challenging Washington's bankruptcy "reform" bill that will keep people in debt for decades.<p>
The MoveOn strategy fits with Dean's ascendancy to the post of DNC chair, where the former "insurgent" now presides over a party whose idea of competing with George W. Bush is to downplay the occupation of Iraq and field two U.S. Senate candidates who share Bush's opposition to abortion rights. And the Democrats' collapse on traditional issues like abortion rights have only accelerated Republican demands to slash the federal budget.<p>
Unfortunately, the Democrats' right turn has pulled the main antiwar organizations along with them. At its meeting last month, the United for Peace and Justice (UFPJ) antiwar coalition moved to focus on what journalist Amy Quinn called  a "multi-year Congressional pressure strategy." In other words, an orientation to the Democratic Party.<p>
UFPJ isn't calling a national protest until September--a year after its last protest, which itself targeted the "Bush agenda" at the Republican National Convention rather than calling for U.S. troops to leave Iraq. Yet 59 percent of those interviewed in a recent opinion poll believe that the U.S. should get out of Iraq.<p>
That sentiment can--and must--be organized. Grassroots activism and clear antiwar politics are the way to rebuild the antiwar movement--not reliance on liberal politicians in the corporate-dominated Democratic Party.<p>


