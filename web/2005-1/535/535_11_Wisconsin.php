<font face="Times New Roman, Times, serif" size="5"><b>Wisconsin state workers push to authorize strike</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Robson, AFSCME Local 171</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 18, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">MADISON, Wis.--After working for 20 months on a contract extension, AFSCME Council 24 bargaining teams and more than 60 local presidents got together March 4 to plan a strike-authorization vote.<p>
Gov. James Doyle, a Democrat, has been insisting that state employees accept a meager 1 percent raise over the two-year contract and pick up $55 each month in health insurance premiums. Currently, 75 percent pay nothing for their health insurance.<p>
Even with other incentives, most state workers would end up with at least a 1 percent pay cut before inflation. Coming on top of only one raise in the last four and a half years--and with many blue-collar workers starting at just $9.79 per hour--not many state employees are willing to consider this.<p>
The meeting came shortly after a successful union decertification vote of the Law Enforcement bargaining unit that represents the State Patrol, University Police and Capitol Police, who made up about 1,000 of Council 24's 26,000 members. With the correctional officers from the state's prisons possibly next in line to decertify the union and rising anger among many rank-and-file members, even many of the most conservative AFSCME leaders realize that something has to happen.<p>
Meanwhile, Gov. Doyle has continued his campaign to eliminate 10,000 state employee positions, at times laying off people who are in federally funded positions in order to reach his goal.<p>
The continued layoffs and contracting out of state jobs to often more expensive private companies have added to the fear and anger among state employees over their inability to get a contract. With management across the state frequently refusing to follow the old contract or work with the grievance procedure, the union has begun to feel powerless to fight on legal terms.<p>
To try to counter the more aggressive tactics from management and make some progress on getting a new contract, participants in the March 4 meeting decided to call for an April 4 strike-authorization vote and an April 21 rally of all Wisconsin public employees at the State Capitol.<p>
After years of no activity in the union and very little rank-and-file organization, organizing for a strike vote will be a challenge. What's more, union leaders' proposals for a vaguely worded ballot might cause confusion among many members about exactly what they are voting for.<p>
But this is an important opportunity for rank-and-file activists to begin to rebuild some organization--and to rebuild our union. If we can get organized, Wisconsin state employees could finally begin to have a voice and make up for years of stagnating wages and deteriorating working conditions.<p>


