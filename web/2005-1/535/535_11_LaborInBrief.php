<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">March 18, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Berkeley, Calif., teachers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jean Whittlesey, Berkeley Federation of Teachers</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BERKELEY, Calif.--In an effort to win a fair contract, members of the Berkeley Federation of Teachers (BFT) began to work to rule February 22.<p>
Teachers and counselors are working only the seven-hour-and-10-minute duty day agreed on in their now-extended contract. This has meant minimal homework for students and, for teachers, no grading or planning at home, extra help after school or extra planning for special events.<p>
The union is now in mediation after two years without a contract. On March 3, 200 out of the 700 teachers and counselors showed their solidarity at a negotiations report meeting by declaring 100 percent participation in the slowdown.<p>
The Berkeley Unified School District (BUSD) has offered zero raises and demanded increased health co-pays from teachers, amounting to a cut in pay. The other issue for teachers is class size, which has reached 35 for many teachers and as high as 48 for physical education classes. The BUSD used a "fiscal emergency" loophole to spend longstanding voter-mandated funds intended to lower class size on other budget items.<p>
In the November 2004 election, the BFT helped campaign for a new ballot measure to lower class size again, the only tax increase that Berkeley voters approved. Now teachers want a guarantee that this new money will be spent for its intended target.<p>
The next mediation session is March 15, after which BFT members will decide what's next. A work-to-rule strategy worked in 1999--let's keep up the pressure!<p>

