<font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Torture and terror American-style</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 18, 2005 | Page 5</font><p>

<font face="Times New Roman, Times, serif" size="3">ON MARCH 1, Dr. Khalid ash-Shaykhli of Iraq's Ministry of Health made a startling revelation at a Baghdad press conference: U.S. forces used mustard gas, nerve gas and other burning chemical weapons against Iraqi civilians in their November assault on the city of Falluja. Ash-Shaykhli added, "[A]ll forms of nature were wiped out in that city. I can even say that we found dozens, not to say hundreds, of stray dogs, cats and birds that had perished as a result of those gasses."<p>
Yet only Arab journalists were present to report this news, which has yet to appear in the mainstream U.S. media.<p>
<i>Il Manifesto </i>journalist Giuliana Sgrena--now in a Rome hospital recovering from bullet wounds from U.S. troops--described the November assault on Falluja as a "massacre." She reported charred bodies, indicating the possible use of napalm, buried by the dozens in mass graves. "The sad story of common graves, which started at Saddam's times, is not yet finished," Sgrena commented.<p>
In addition, Sgrena reported on the torture of ordinary Iraqis at the hands of U.S. forces, including the gruesome experiences of Mithal al-Hassan, detained for 80 days and tortured at Abu Ghraib. <p>
Al-Hassan described women prisoners forced to separate feces from urine with their hands and to drink water from a toilet bowl. When asked whether there were cases of rape, she replied that there were, but refused to discuss them, citing cultural customs. From her cell, she could hear children screaming as they were being tortured with dogs.<p>
Al-Hassan's description of child torture offers a new dimension to Brig. Gen. Janis Karpinski's testimony during the investigation of torture at Abu Ghraib last May. Karpinski described an 11-year-old boy at Abu Ghraib who told her "he really wanted to see his mother, could he please call his mother. He was crying."<p>
As it turns out, the troops who fired on Sgrena's vehicle on March 4 were from the 3rd Infantry Brigade--accused of committing various war crimes last year, "from rape to hog-tieing and beating up an Iraqi detainee," according to Britain's <i>Guardian </i>newspaper. The Army investigation, launched in April, was concluded in July without charges filed, because "there was insufficient evidence to prove or disprove the allegations." <p>
But the Army's inability to discover evidence of war crimes is apparently from its own lack of effort. The American Civil Liberties Union has made public 1,200 pages of abuse investigations obtained under the Freedom of Information Act, noting that none of the 13 cases of suspected abuse--including numerous instances of U.S. troops firing on civilians at military checkpoints--resulted in charges. The Army's investigation into the rape accusation against four 3rd Infantry Brigade soldiers, for example, was halted due to "lack of evidence"--without even interviewing the two alleged Iraqi rape victims.<p>
Another case contained in the documents involves a DVD made by members of a Florida National Guard unit while serving in Iraq last year. The DVD can be <a href="http://www.palmbeachpost.com/news/content/news/video/haj1.html" target="_blank">viewed online</a> at the <i>Palm Beach Post </i>Web site. Called "Ramadi Madness and the Haj Files," it shows a soldier holding up the hand of a dead Iraqi to wave "hi" to the camera; another soldier kicking a wounded Iraqi prisoner; and a soldier taking the butt of his rifle to a detainee. The Army's internal investigation, however, cleared the soldiers of wrongdoing.<p>
Perhaps most disturbing about "Ramadi Madness" is its "day in the life" exposure of occupied Iraq--including a scene of soldiers joyriding down a Ramadi street, whooping and hollering at Iraqi pedestrians to "get out of the way" as their vehicle swerves threateningly close. <p>
Two years ago, the U.S. invaded Iraq, using the claim that Saddam Hussein had "gassed his own people" and buried them in mass graves, while the torture of ordinary Iraqis was widespread. Now the U.S. can claim the same.<p>
The war on Iraq is not merely a war based on lies and hypocrisy. It is an imperialist invasion of a sovereign nation, aimed at humiliating its population. And imperial arrogance, apparently, knows no bounds.<p>

