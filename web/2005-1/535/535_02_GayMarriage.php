<font face="Times New Roman, Times, serif" size="5"><b>Gay-marriage ban overturned in Calif.</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 18, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">"SAME-SEX marriage cannot be prohibited solely because California has always done so before."<p>
San Francisco Superior Court Judge Richard Kramer--a Catholic Republican appointed by a former GOP governor--handed supporters of equal marriage rights a victory as <i>Socialist Worker</i> went to press, overturning California's ban on same-sex marriage as unconstitutional. "It appears that no rational purpose exists," he wrote, "for limiting marriage in this state to opposite-sex partners."<p>
The ruling resulted from a lawsuit brought against the state by the City and County of San Francisco and a dozen same-sex couples who were married last year after Mayor Gavin Newsom--under pressure to deliver following a tight run-off election against Green Party candidate Matt Gonzalez--began issuing marriage licenses to same-sex couples in defiance of state law. More than 4,000 same-sex couples flocked to City Hall and were married before the state Supreme Court ordered the city to stop issuing the licenses.<p>
In this latest case, the state argued that it was fine to define marriage as between a man and woman--as long as same-sex couples enjoyed virtually the same rights as married couples. But as Kramer wrote, "The idea that marriage-like rights without marriage is adequate smacks of a concept long rejected by the courts: separate but equal."<p>
Unfortunately, Kramer also stayed his ruling for 60 days in order to give opponents time to appeal the decision--which they undoubtedly will. Still, activists can use this new victory to renew the fight for equal rights in California.<p>

