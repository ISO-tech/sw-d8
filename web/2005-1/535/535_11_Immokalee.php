<font face="Times New Roman, Times, serif" size="4">Coalition of Immokalee Workers wins a pay raise</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Victory over Taco Bell</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 18, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">IMMOKALEE, Fla.--The Coalition of Immokalee Workers (CIW) ended its more than three-year boycott of Taco Bell on March 8, after the fast-food company agreed to pay the penny-per-pound surcharge demanded by tomato pickers.<p>
"This is a great step in the struggle to demand fair food," CIW co-director Lucas Benitez told <i>Socialist Worker.</i> "The next step is to continue working for change in the whole industry."<p>
The CIW, which has some 2,500 members, was formed in 1993 by tomato and citrus workers in Immokalee, Fla., to take on horrific working conditions and wages in Southern Florida's fields. The mostly immigrant farmworkers there work long hours in the hot sun, with few breaks, little drinking water and low wages, enduring regular harassment and abuse by supervisors.<p>
Past CIW actions include a strike of 4,000 workers in 1995 that forced growers to back down from attempts to slash wages. CIW's 1997 living-wage campaign, which included work stoppages, marches on the Florida state capitol and a hunger strike, won industry-wide raises of between 13 and 25 percent. <p>
In 2001, the CIW launched a national boycott of Taco Bell, which buys its tomatoes from Six L's Packing Co., which runs the fields in Immokalee. The campaign included a cross-country Taco Bell Truth Tour. "Organizing of the workers has been the most important aspect because they are really the engine behind this campaign," said Benitez. "We've been traveling around the country for the past four years."<p>
CIW members enjoyed the support of union, student and community activists in many cities, where they organized actions to welcome the Truth Tour to town.<p>
This week, Taco Bell announced that it will begin paying 1 cent more for each pound of tomatoes that workers pick, raising pay for workers from 40 to 45 cents for each 32-pound bucket they pick to 72 cents. Taco Bell also agreed to work with the CIW to improve working conditions. Yum! Brands, of which Taco Bell is a subsidiary, announced that it is adding language to its Supplier Code of Conduct that forbids indentured servitude, a practice that is all too common in this industry.<p>
CIW members plan to use this recent win at Taco Bell to call on other companies to follow their lead.<p>

