<font face="Times New Roman, Times, serif" size="4">Students oppose recruiters across the U.S.</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The struggle spreads</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 18, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">IN CITIES across the country, student activists are organizing to kick military recruiters off campus--and responding to administrators' attempts to keep them quiet.<p>
At San Francisco State University (SFSU), hundreds of students and faculty protested the military recruiters' appearance at a career fair on March 9, forcing them to leave an hour early. With chants of "Racist, sexist, antigay, U.S. military go away!" protesters underscored the Pentagon's overtly antigay policies--such as "don't ask, don't tell"--which put it in violation of the SFSU's anti-discrimination policy.<p>
Protesters surrounded the recruiters' tables, making it impossible for anyone to get near them--and peppered the military's representatives with questions. Military pamphlets, key chains and other promotional items disappeared from the tables and ended up thrown in the air like confetti.<p>
During the two-hour protest and the sit-in and teach-in that followed, protesters--some wrapped in Iraqi, Palestinian and rainbow flags--spoke out about their friends and family who were recruited to the military out of college and high school, and are now deployed to Iraq. At 2 p.m., about an hour earlier than scheduled, the recruiters packed up and left.<p>
But the fight isn't over. The administration is threatening the rights of students on campus with sanctions against student groups and suspension of individuals for taking part in the protest.<p>
Sean O'Neill, a veteran who returned from Iraq last year after serving with the Marines, spoke out in defense of the SFSU students. "Do students have the right to protest?" he said. "Of course they do...As a vet, I don't take any offense. Anyone who doesn't want me over there is a friend in my book."<p>
Meanwhile, at Cornell University in Ithaca, N.Y., antiwar activists confronted military recruiters at a career fair March 3. When asked by protesters if the military would allow them to be openly gay, the recruiters had no choice but to state their discriminatory policy. Afterward, activists filled out Campus Discrimination Report forms, flooding the administration with 60 complaints in one day--they usually receive six a month.<p>
In Burlington, Vt., antiwar activists crashed an Air Force meeting March 7 designed to recruit University of Vermont (UVM) nursing students who were about to graduate. One protester presented an article from the <i>New England Journal of Medicine </i>that pointed to the involvement of medics in torture practices. The next day, Students Against War activists protested military recruiters at the UVM career fair--where there were tables sponsored by the ROTC, the Army and the National Guard.<p>
The national Campus Antiwar Network, Left Hook and the Global Resistance Network have called for a week of campus protests from March 14-18--leading up to the international day of protest on March 19.<p>
<i>Cynthia Little, Justine Prado, Rebekah Ward and Susie Wasserstrom contributed to this report.</i> <p>

