<font face="Times New Roman, Times, serif" size="5"><b>Bring them home now!</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 18, 2005 | Pages 1 and 2</font><p>

<font face="Times New Roman, Times, serif" size="3">"I CAN'T even describe in words how horrible it is--the feeling of helplessness about how far away they are, and the level of danger they're in every day." <p>
That's how Colleen McLaughlin talks about the tension she lived with every day while her son, a member of the Vermont National Guard, was deployed in Iraq. Her words speak for the feelings of people throughout the U.S.--the family and friends of those sent to Iraq to kill and be killed in the U.S. war and occupation.<p>
The supporters of war in Washington said that an invasion of Iraq was necessary because of Saddam Hussein's "weapons of mass destruction." They accused Iraq of supporting al-Qaeda. They promised that U.S. soldiers would be welcomed by grateful Iraqis, thankful that their country had been "liberated." And now, they say that the occupation of Iraq is promoting the spread of democracy across the Middle East.<p>
These were lies from the beginning, says McLaughlin. "I see it as a war of policy," she said. "This was a policy that they had argued for from the early 1990s. It was basically a shift from diplomacy and deterrence to a policy of pre-emptive military aggression--to spread democracy, they say, but as we see it, to spread American interests abroad through the use of military force.<p>
"My son has given me permission to quote him, and he believes it's actually a war for oil--that we're not there to help the Iraqi people, that we've trashed the country, and we're there for our own interests."<p>
McLaughlin isn't alone in reaching these conclusions. Bitterness has spread among soldiers themselves--with a recent military magazine poll showing that 60 percent of troops don't approve of the war, according to one Iraq war vet. More service members are following the examples set by people like Camilo Mejia, Pablo Paredes and Kevin Benderman--and are refusing to deploy to Iraq to fight in what Paredes calls "an illegal war for oil."<p>
Over the past two years, military family members have taken an increasingly prominent role in the antiwar movement--all the more so last year, when activities slackened because of the presidential campaign. On March 19, thousands of people--many of them with relatives deployed overseas--will travel to Fayetteville, N.C., home of the Army's Fort Bragg, for an antiwar demonstration, as part of an international day of protest.<p>
In Burlington, Vt., McLaughlin and the local chapter of Military Families Speak Out were central to the campaign to win antiwar resolutions--which passed in nearly every community where they were voted on during Vermont's annual town meeting day earlier this month.<p>
McLaughlin dismisses the claim that opponents of war should stay silent to "support the troops." "It's nonsense to say that if we oppose the war, we're not supporting the troops," she says. "We are the voice of support for the troops, not the folks who are sending them there for a war based, as we know, on lies."<p>
McLaughlin also rejects the belief, voiced even by some in the antiwar movement, that the U.S. has a responsibility to maintain a military presence in Iraq, to prevent chaos. "To adopt that attitude gives a green light for perpetual warfare," she says. "If people in the antiwar movement adapt that attitude--if we broke it, then we have to fix it--then that what will happen in the next war that the Bush administration instigates. I think the American presence is the problem, not the solution."<p>
And, she adds, "All of the billions of dollars that President Bush is asking for to continue the occupation should go to the Iraqi people for reparations and to repair their infrastructure." <p>
The March 19 demonstrations can be a step toward rebuilding a broad antiwar opposition that can send our message to all the politicians: Bring the troops home now!<p>


