<font face="Times New Roman, Times, serif" size="5"><b>Police in Texas celebrate club fire</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 18, 2005 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">I WAS horrified to learn that on February 18, Austin, Texas, police officers let a Black-owned nightclub with primarily Black clientele burn to the ground. Rather than respond to calls for assistance, at least nine police officers spent two hours exchanging computer messages celebrating and making fun of the fire.<p>
One officer wrote, "Burn baby burn"--to which another officer responded, "My thoughts exactly." Showing just how much contempt these officers had for the Black nightclub-goers, a dispatcher wrote, "I have some extra gasoline if they need it." <p>
Yet another officer wrote, "U can smell from [Interstate] 35. It is the smell of victory," and "My nite is made. I just had a lady ask me if it was burning. I said, 'Yep.' She was upset. I was enthralled." Another dispatcher wrote, "You hear that Midtown is on fire!! The roof of a club...That's funny! Gives a whole new meaning to the roof, the roof is on fire."<p>
In response to the outrage of the Black community in Austin, nine police officers have been suspended for 15 days, told by the police chief that their actions were "inappropriate." This incident was more than an accident or an aberration, however--and certainly amounts to more than "inappropriate" behavior. This type of racist policing is no less than a violent hate crime.<p>
Ku Klux Klan-like police brutality will only escalate if it goes unchallenged. As this sick instance of institutional racism in Austin shows, there is a crying need for a new civil rights movement in this country to confront the racist vigilante cops that terrorize minority communities throughout the U.S.<br>
<b>Sarah Hines, </b>New York City<br>

