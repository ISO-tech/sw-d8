<font face="Times New Roman, Times, serif" size="5"><b>Did Bush steal the White House again?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 14, 2005 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">PEOPLE WHO saw Michael Moore's <i>Fahrenheit 9/11</i> will recall the scene of Congressional Black Caucus members requesting a single member of the Senate to support their calls for investigations into the stolen election in Florida that put Bush in the White House in 2000.<p>
Last week, that scene was re-enacted over disputed results in Ohio last November. Only this time, instead of responding with craven silence, Democratic senators--or at least one of them, Barbara Boxer of California--agreed to support a challenge to Bush getting Ohio's electoral votes.<p>
But after a few minutes of speechmaking on the importance of electoral reform, the Congress went ahead and dismissed the complaints about Ohio, certifying Bush as the victor in that state, and therefore the next president.<p>
Bush's opponent, John Kerry, was AWOL--having arranged for a visit to troops in Baghdad to coincide with the certification vote. In the Senate, only Boxer voted to pursue an investigation. In the House, 81 Democrats voted with the Republicans to dismiss the objections--and 80 House Democrats didn't even bother to show up.<p>
As <i>Salon's</i> Tim Grieve described the spectacle, "There was no sense of history being made, no sense that anything was really happening at all." In the end, the Democrats' performance in 2004 was only slightly improved over their dive in 2000.<p>
For more than two months since the November election, a number of activists have documented voter intimidation and dirty tricks carried out by the pro-Bush hacks running the Ohio election--starting with Republican Secretary of State Ken Blackwell--to suppress the Democratic vote. Similar hijinks took place in Florida, Georgia and other states.<p>
But this doesn't prove that Bush stole the election like he did in 2000--or that "Kerry really won," as hundreds of Internet activists argue today.<p>
If there was anything notable about the election and the dirty tricks involved in it, it was how similar Election 2004 was to every other election organized in the U.S. Alone among major industrial democracies, the U.S. leaves election administration in the hands of corrupt and partisan local officials--and vote-counting to the private manufacturers of voting machines. And the Electoral College--a relic of the pre-Civil War era--chooses the most important office in the country, not a nationwide popular vote.<p>
That's why even if it could be proved that Kerry really won Ohio, his victory would be tainted--for having been won in the Electoral College, while losing the popular vote. While the left was right to denounce Bush as illegitimate when he became president after losing the popular vote in 2000, it would have no moral high horse to ride if Kerry had become president under similar circumstances.<p>
The dirty secret of American "democracy" is that neither major political party wants elections to be truly democratic and free. Instead, they want a system that predictably produces victories for their corporate money-soaked candidates.<p>
<i>Counterpunch's</i> Alexander Cockburn is correct when he argues: "Do I think the election was stolen? No more than usual. The Democrats are getting worse at it and the Republicans better. Back in 1960, it was the other way round. The best-documented stolen election in history is probably the one that put Lyndon Johnson in the U.S. Senate. Next came the one that gave JFK the White House. So, for sure, there's vote suppression in Ohio and Florida. I don't think it made the crucial difference."<p>
Many of the liberals who complained about voter suppression in 2004 were also the same people who spent millions of dollars and thousands of hours to make sure that Ralph Nader would not appear on ballots across the country. And a brewing scandal in California alleges that Democratic Secretary of State Kevin Shelley diverted some of the more than $3 billion in federal money appropriated to modernize the state's voting system to cronies and his own partisan self-promotion.<p>
The Democrats' sorry performance throughout Election 2004 should show just how wrong is anyone who thinks that a party named after democracy would actually be interested in it.<p>

