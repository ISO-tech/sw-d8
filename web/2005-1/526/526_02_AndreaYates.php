<font face="Times New Roman, Times, serif" size="5"><b>Texas court orders new trial for Yates</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 14, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">IN A surprise move, a Texas appeals court last week granted a new trial to Andrea Yates, the woman convicted in 2002 of drowning her children in a bathtub.<p>
Despite the fact that Yates suffered from severe mental illness--she explained to police that she had killed her children to save them from Satan because she was a "bad mother"--and had twice attempted suicide, Texas prosecutors showed no mercy to her or her devastated family.<p>
Proving that they would sink to any level to get a conviction, prosecutors relied on the testimony of psychiatrist Dr. Park Dietz, who told the jury that Yates--while psychotic at the time of the murders--knew right from wrong, making her ineligible for an insanity plea under Texas law. <p>
Dietz even suggested to the jury that Yates might have staged her defense. Dietz--a consultant for the television show <i>Law &amp; Order, </i>which Yates reportedly liked to watch--testified that "there was a show of a woman with postpartum depression who drowned her children in the bathtub and was found insane, and it was aired shortly before the crime occurred."<p>
But it turns out that Dietz was lying. No such episode ever existed.<p>
Incredibly, this information about Dietz's lies came to light during the trial--after Yates had been found guilty, but before she had been sentenced--but the court denied a defense request for a mistrial.<p>
Last week, the appeals court ruled that Dietz's false testimony could have affected the judgment of the jury--and that Yates should therefore receive a new trial. Prosecutors immediately complained about the ruling and vowed to reverse the court's decision. <p>
While they appeal, Andrea Yates will remain in prison--continuing to be punished for her mental illness.<p>

