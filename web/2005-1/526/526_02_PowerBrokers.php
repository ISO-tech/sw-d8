<font face="Times New Roman, Times, serif" size="4">Republicans spending $40 million on Bush inauguration</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Party for the power brokers</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 14, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">IT'S HIS party, and he'll spend millions on it if he wants to. The occupation of Iraq, the tsunami disaster relief effort, debate over plans to dismantle Social Security--George Bush and friends aren't letting any of that stop them from partying with their corporate backers when Bush is inaugurated on January 20. <p>
The Republican faithful are getting ready to celebrate another four more years in the White House--with no expense spared. The official festivities, organized around the theme "A Vision of America," will include nine inaugural balls, plus three "candlelight dinners"--where corporate donors who pay $100,000 or more will be given the chance to meet face to face with the Bushes and Cheneys. <p>
AT&amp;T, Bank of America, Bristol-Myers Squibb, Pepsi-Cola, Tyson Foods, Goldman-Sachs and the energy company Cinergy are among the corporate giants to buy their way into the Bush party. Individuals who raised large amounts of money for the Bush presidential campaign can also buy face time with the Commander in Chief--for a mere $5,000. As Bruce Bialosky, a California accountant who raised more than $100,000 for the Bush campaign, told the <i>New York Times,</i> the cost of the inauguration is the "price of playing the game, I guess."<p>
In all, the inauguration is expected to cost an estimated $40 million--which is especially sickening considering that the Bush administration had originally promised less than half that sum in aid to the victims of December's tsunami.<p>
But the $40 million doesn't even include the cost of dozens of parties being sponsored and funded by lobbyists--where Corporate America will get the chance to wine and dine Washington power brokers.<p>
To add insult to injury, the Bush gang is making a nod to the military with a special "Commander-in-Chief Ball"--open to some invited service members and their families who recently returned from Iraq and Afghanistan, or will soon be deployed there. If the Bush administration really wanted to "honor" the troops, it might have considered purchasing the body and vehicle armor that soldiers have been demanding--or increasing military pay and veterans' benefits.<p>
While the tab for the inaugural festivities will be picked up largely by Corporate America, taxpayers will have to foot the bill for some of the tightest security that Washington has ever seen. Not only will as many as 2,500 police officers from across the country be brought in for the event--twice the number compared to the 2001 inauguration--but the Secret Service has plans for 4,000 military troops. <p>
Sturdier barriers and even more pedestrian checkpoints than in 2001 are being set up--to protect Bush from any embarrassing replays of his last inauguration, when he had to forego a walk down Pennsylvania Avenue in favor of a secluded limo ride away from egg-throwing protesters.<p>
But antiwar activists and others are planning on making their presence felt anyway on Inauguration Day.<p>
The Campus Antiwar Network, for example, voted at its November convention to attend the protests and is organizing on several college campuses to send people to D.C.<p>
A number of demonstrations have been called for the days leading up to the inauguration. International ANSWER is planning for a convergence on Pennsylvania Avenue, and the D.C. Anti-War Network (DAWN) has called for student walkouts, as well as a rally--endorsed by United for Peace and Justice--at Malcolm X Park, to be followed by a march toward the White House. <p>
The International Socialist Organization will be hosting a counter-inaugural town hall meeting at the First Congregational Church for after the protests--featuring Military Families Speak Out activist Stan Goff, exonerated death row prisoner Shujaa Graham and <i>International Socialist Review</i> contributor Sherry Wolf.<p>
For those not able to protest Bush in D.C., local counter-inaugural demonstrations are planned for San Francisco, Los Angeles, Seattle, Chicago, New Orleans and other cities across the country.<p>
Together, we can show Bush that we'll stand up to his right-wing agenda.<p>

