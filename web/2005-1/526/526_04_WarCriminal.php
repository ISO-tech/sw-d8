<font face="Times New Roman, Times, serif" size="5"><b>There's a war criminal in the White House</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 14, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
George Bush is a war criminal and needs to be tried as such. The U.S. Congress is now using economic extortion to make U.S. soldiers and officials exempt from being tried for war crimes by the International Criminal Court. <p>
Bush has overseen the death of thousands of innocent Afghans and Iraqis, women, children and men. The Brookings Institute has stated that 10,000 to 27,000 civilians have died because of the U.S. invasion in Iraq. The death toll in Afghanistan is not even counted. The Falluja campaign left a city of 300,000 deserted, but not before the U.S. shut off its water supply and electricity for weeks in advance, causing the deaths of more than 800 civilians. <p>
The responsibility for this lies squarely on the hunching shoulders of the criminal who calls himself president. <p>
You'll hear many figureheads repeat the common refrain: "We have to support our troops." This rhetoric rings very hollow to me. Putting a $4.99 yellow ribbon on your car doesn't say that you support the troops. It says, "I approve of the war, no matter what U.S. soldiers are doing."<p>
The footage of the Marine that shot the unarmed man lying on the floor of a mosque was horrific. That's just one example that happened to be caught on film. What else is happening on the ground? What other atrocities are being committed? <p>
The popular idea is that we can't leave Iraq because we have to help the Iraqis rebuild their country. Easy--withdraw U.S. troops, pay reparations for the demolition of their entire infrastructure and let them build their own country. Saying that Iraqis cannot rebuild their nation is a racist idea that recalls the days of colonizing. <p>
If people are genuinely interested in the well being of U.S. soldiers, make a call to bring them home now! Their health is best served not fighting a war for oil and empire.<br>
<b>Shane Johnson,</b> Cincinnati<p>

