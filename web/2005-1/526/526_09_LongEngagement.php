<font face="Times New Roman, Times, serif" size="5"><b>Lost in the horror of the First World War</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Donny Schraffenberger</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 14, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>A Very Long Engagement, </b></i>directed by Jean-Pierre Jeanet, starring Audrey Tautou and Gaspard Ulliel. In French with subtitles.</font><p>

<font face="Times New Roman, Times, serif" size="3"><i>A VERY Long Engagement </i>is well worth the price of admission.<p>
The movie is set in France during the First World War and its aftermath. A young woman, Mathilde--played by Audrey Tautou, who also starred in director Jean-Pierre Jeanet's 2001 film <i>Am&eacute;lie</i>--has lost her fianc&eacute; in the war, yet refuses to believe he is dead.<p>
She hires a private detective to help her search for him. Her journey exposes the rottenness of the war, and the awful pain that was inflicted on the common soldiers and their families by the generals and politicians.<p>
Almost 2 million French soldiers were killed in the "Great War." The war lasted four years, and is starkly remembered for its senseless slaughter. Even today, the common image of soldiers going over the top of the trench to be instantly killed by machine gun and artillery shells still haunts our imaginations.<p>
At the Battle of the Somme, the British had 60,000 casualties in one day. The war was so horrific that many soldiers became "shell shocked," some so sick of war that they purposely walked into no man's land--the land between the two army's trenches--where they would surely be shot and end their suffering.<p>
Others took less final, though just as drastic, steps. Soldiers shot themselves to feign battle wounds. If caught or even wrongly accused of self-mutilation, a soldier could be court-martialed, imprisoned and even executed.<p>
<i>A Very Long Engagement </i>is about French soldiers that have seen enough of war and want to get out of it. One condemned soldier fruitlessly tries to convince his comrades that the war is for the rich and they are only cannon fodder, or "food for the cannons." When he repeatedly fails to convince them, he resorts to shooting himself.<p>
In the film, five soldiers are accused of self-mutilation and are given the sentence of death. But instead of being shot by a firing squad, they are pushed into no man's where they will certainly be killed.<p>
This sentence appalls most of the French soldiers. They can identify with the victims. They too are sick of the war.<p>
In 1917, sections of the French Army mutinied after a stupidly planned and bloody offensive decimated their ranks. Also in 1917, Russian soldiers refused to fight, left the front and abandoned the war. In 1918, German sailors mutinied and took part in the German Revolution that finally sealed the fate of the World War.<p>
<i>A Very Long Engagement </i>accurately portrays a French general who is indifferent to the fact that his men are being slaughtered. While they are being butchered on the battlefield, he is living in a mansion and enjoying the good life. While most generals come from the upper crusts of society, most rank-and-file soldiers are workers and farmers that do the real fighting and dying while the top brass get their fame in the history books.<p>
I don't want to ruin the film for you, so let's leave it at this--<i>A Very Long Engagement </i>is an intoxicating mixture of suspense, war and love.<p>

