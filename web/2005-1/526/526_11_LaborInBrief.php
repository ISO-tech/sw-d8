<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">January 14, 2004 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Caterpillar</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><p>

<font face="Times New Roman, Times, serif" size="3">PEORIA, Ill.--Why did United Auto Workers (UAW) leaders pressure members at Caterpillar vote to accept devastating concessions despite record profits for the company?<p>
"The choice is between voting no or not voting at all," a 30-year worker told <i>Socialist Worker </i>at the UAW Local 974 vote here. "I can't in good conscience vote yes." But in the end, a majority--the UAW didn't immediately release vote totals--voted to accept a contract that introduces two-tier wages and massive increases in health care costs for both the 9,000 current workers and retirees.<p>
Burdened by the defeat of two long strikes in the 1990s--and the unwillingness of UAW International officials to put up a real fight--even old UAW stalwarts were ready to throw in the towel. "You can only fight for so long, and then you get tired," Dean, a 31-year veteran worker, told <i>Socialist Worker.</i> "If we voted it down, they would have implemented an agreement that would have been worse."<p>
Local 974 officials used the local press to maximize such fears, predicting that Caterpillar would permanently replace UAW members if they went on strike.<p>
While the company would certainly have been aggressive, the fact is that the UAW--despite holding a strike authorization vote--never intended to fight. The union, which pushed concessions in the 1980s to make companies more competitive, this time made givebacks to a company that dominates its industry worldwide.<p>
In any case, many longtime Caterpillar workers had no confidence in the UAW International, which forced an end to a strike in 1995 by cutting off strike benefits.<p>
Since a contract was approved in 1998, the company has used concessions to outsource jobs--often to in-plant contractors. UAW membership at Cat has fallen from 14,000 to 9,000 since then.<p>
About 1,700 of the UAW workers are "supplementals"--hired at 70 percent of top wage rates that were to gradually increase to scale. They got no benefits or vacation--but paid UAW dues. Under the new contract, they'll become permanent employees with benefits--but as "new hires" with lower pay.<p>
The new scale begins at $10 per hour at the lowest labor grade--half the pay of current employees. But Bob Morris, a young welder, voted for the deal even though it cut his pay, since his only health insurance was "a hope and a prayer."<p>
Divisions between veteran workers, scabs, supplementals and contract employees helped Caterpillar carry out a classic divide-and-conquer operation to sell this contract--with the UAW International's help. <p>
UAW President Ron Gettelfinger sold the deal as "the best possible contract under very difficult circumstances." That should be a call to arms for every UAW member who wants to rebuild a fighting union.<p>

