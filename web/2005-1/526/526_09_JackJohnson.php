<font face="Times New Roman, Times, serif" size="4">The legacy of boxer Jack Johnson</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Hitting back against racism</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Dave Zirin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 14, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Jack Johnson's Unforgivable Blackness,</b></i> a documentary by Ken Burns, showing on PBS stations January 17-18. See local listings for details.</font><p>

<font face="Times New Roman, Times, serif" size="2"><b></b></font>JACK JOHNSON, the first Black, heavyweight-boxing champion of the United States, was described recently by <i>Sports Illustrated </i>as having "Muhammad Ali's charisma, Mike Tyson's power and Don King's cunning." He resides today somewhere between legend and myth, which is why the new documentary <i>Unforgivable Blackness </i>by Ken Burns is such an amazing contribution to any student of sports, history or struggle.<p>
Burns has achieved renown for his meticulous--some would say overly meticulous--documentaries about the Civil War and Major League Baseball. Now Burns tells Johnson's story of a Black heavyweight champion in an era of white supremacy--from a perspective that is unabashedly antiracist.<p>
Through the use of rare archival footage and uncovered texts, we learn about Jack Johnson's childhood as the son of former slaves who insisted that he and his six brothers and sisters know how to read and write. We learn about his start in boxing in the "Battle Royal," a practice in the Jim Crow-era South in which a group of African American boys were blindfolded by white men and told to box bare-knuckled until only one boy was left standing; the winner receiving a handful of tossed coins.<p>
By the age of 18, Johnson was traveling the country as a boxer and earning $5 to $10 a night. By 1902, Johnson had won at least 27 times and was making as much as $1,000 a night, but could not get a white champion to fight him.<p>
Burns never shies away from the social context of Johnson's success. Between 1901 and 1910, 754 African Americans were lynched. This was also the era of a deeply racist pseudo-science that espoused that, not only were African Americans too stupid to succeed in sports, they were also too lazy.<p>
When Johnson finally won the title, his victory caused an ideological crisis throughout the U.S. The media whipped up a frenzy around the need for a "A Great White Hope" to restore order to the world.<p>
Former champion Jim Jeffries came out of retirement and said, "I am going into this fight for the sole purpose of proving that a white man is better than a Negro." At the fight, which took place in 1910, the ringside band played a song called, "All coons look alike to me," and promoters led the all-white crowd in the chant "Kill the nigger."<p>
But Johnson was faster, stronger and smarter than Jeffries, knocking him out with ease. After Johnson's victory, there were race riots around the country--in Illinois, Missouri, New York, Ohio, Pennsylvania, Colorado, Texas and Washington, D.C. Most of the riots consisted of white lynch mobs attempting to enter Black neighborhoods, and Blacks fighting back.<p>
This reaction to a boxing match was the most widespread simultaneous racial uprising in the U.S. until the riots that followed the 1968 assassination of civil rights leader Dr. Martin Luther King, Jr. Black leaders, such as Booker T. Washington, pushed Johnson to condemn African Americans for rioting.<p>
But Johnson remained defiant. He spoke out on all issues of the day, married white women, and as result, faced harassment and persecution for most of his life.<p>
Johnson was forced into exile in 1913--and later prison--on the trumped-up charge of transporting a white woman across state lines for prostitution. He was released on July 9, 1921, at the age of 43. Johnson never again fought for the heavyweight title and spent his later years recounting his glory in a Times Square sideshow and at county fairs.<p>
Throughout this whole period, Johnson's greatest sin, as the title of the film suggests, is that he was a strong, loud Black man during an era of white supremacy. Burns never shies away from Johnson's faults and vices, but he is always clear that Johnson and his "unforgivable Blackness" should be celebrated and not condemned.<p>
Today when "Driving While Black" is a daily reality for millions, and Blacks suffer mass incarceration, learning about Johnson and his era can inspire us toward the kind of defiance we must bring to our own era.<p>

