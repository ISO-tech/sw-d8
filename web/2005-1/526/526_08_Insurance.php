<font face="Times New Roman, Times, serif" size="5"><b>Sigh of relief for insurance vultures </b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Taylor Johnson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 14, 2005 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">SURVEYING THE immense loss of life and structural damage in coastal Southeast Asia after the tsunamis, the global insurance industry had a perverse response. Relief.<p>
While survivors still searched for lost relatives and scrambled after food and clean water, insurers were celebrating that the tragedy wouldn't have a "material impact" on their 2004 profits. At least 5 million people have been made homeless. But because few have insurance, the industry won't have to foot the bill for reconstruction of their houses, shops or fishing boats. <p>
The world's two largest reinsurers (who provide backup catastrophe coverage for primary insurers) both reported that their expected losses would total only around $100 million or less, and primary insurers say they're only moderately affected. Compared with the $27 billion insurance payouts from the hurricanes that hit Florida last year, losses to the tsunami will be a drop in the bucket. <p>
This isn't just because Florida has a more developed coastline. U.S. developers, homeowners and businesses can also afford to purchase insurance. Insurance premiums per person totaled $3,600 in the U.S. in 2003. In Thailand, they amounted to only $80, and in Sri Lanka, Indonesia and India, they ranged between $12 and $16.<p>
The few damages that insurers will cover in these countries won't help local people. Commercial property insurance on mega-resorts and coverage for business interruption and cargo ships will pay out to the wealthy few and multinational corporations.<p>
Meanwhile, no coverage is affordable--or even available--to the millions of people who live in the areas at high risk for earthquakes, floods and tropical cyclones. <p>
According to Swiss Reinsurance's calculations last year, before the tsunami, the death toll of the 40 most fatal natural disasters since 1970 was 1.15 million people. Their total cost to the insurance industry was $2.9 billion. By contrast, the most expensive natural catastrophes caused more than $138 billion in insured losses--but only killed 11,800 people. <p>
It's no coincidence that the most expensive disasters were all concentrated in the U.S., Europe and Japan--and the most deadly disasters occurred in some of the world's poorest regions, such as Bangladesh, China, Iran, Peru, Guatemala, Armenia and Colombia. <p>
The majority of deaths in the 10 most fatal events of the last 30 years were due to collapsed buildings that had been constructed with poor-quality materials. Developers make huge profits by building with cheap materials where building codes don't exist or are rarely enforced. And because these buildings are more vulnerable to collapse, they are more difficult, if not impossible, to insure. <p>
This vicious cycle leaves the world's poorest and most vulnerable paying--often with their lives--while international business elites line their pockets.<p>
