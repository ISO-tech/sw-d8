<font face="Times New Roman, Times, serif" size="4">Attorney general nominee has it easy at Senate hearing</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Kid-glove questions for Gonzales</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 14, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE ATMOSPHERE in the hearing room was chummy as the Senate Judiciary Committee held a confirmation hearing for Alberto Gonzales, George W. Bush's nominee to be attorney general. Sen. Arlen Specter (R-Pa.) had Gonzales introduce his wife and sons, and then Sen. Patrick Leahy (D-Vt.) praised Gonzales for working his way up from a modest background to attend Harvard Law School and eventually become Bush's chief White House counsel.<p>
Gonzales was expected to deny his role in finding legal excuses for the torture of detainees in Guant&aacute;namo Bay, Afghanistan and Iraq. But he didn't even bother. <p>
Gonzales refused to condemn torture techniques associated with U.S. forces--such as mock executions or forcing detainees under water until they nearly drown--saying that "it is not my job" to decide if such interrogation techniques are proper.<p>
When one senator asked Gonzales if "U.S. personnel [can] legally engage in torture under any circumstances," Gonzales replied, "I'd want to get back to you on that." Then the senator moved on--without even drawing attention to the fact that Gonzales wasn't answering what was supposed to be the main question of the hearings.<p>
Democrats on the committee did their best to make him feel at ease. Despite an indignant speech by Sen. Ted Kennedy (D-Mass.) and some pointed questioning by Sen. Joseph Biden (D-Del.), the Democrats knew that Gonzales would be confirmed as attorney general, no matter how many questions he evaded. "I love you, but you're not very candid so far," said Biden to laughter in the gallery.<p>
These chuckles among friends are out of keeping with the record of the man who was being questioned.<p>
Gonzales has long served as a loyal member of Bush's inner circle. When Bush was governor of Texas, Gonzales was his general counsel. He reviewed the cases of more than a third of the 152 people that Bush executed--and advised Bush to go ahead in every case, regardless of any claims of innocence, ineffective counsel or other factors.<p>
Once Bush became president, Gonzales took a similar job at the White House--and reportedly helped to craft the "legal" arguments of the Bush administration that justified the torture techniques used at Abu Ghraib.<p>
Gonzales denies that he ever characterized the Geneva Conventions outlining the rights of prisoners of war as "obsolete" and "quaint." But this is exactly why several top-ranking military officials challenged his nomination, including Army Gen. John Shalikashvili, former chair of the Joint Chiefs of Staff, and retired Brig. Gen. James Cullen, a former chief judge of the U.S. Army's Court of Criminal Appeals. They argue that if the U.S. disregards the rights of prisoners of war, captured U.S. soldiers could be exposed similar abuses.<p>
Last week--in a transparent attempt to make Gonzales' hearing go more smoothly--the Justice Department repudiated the memo that, at Gonzales' urging, re-defined torture as only those techniques that produce pain "of an intensity akin to that which accompanies serious physical injury such as death or organ failure." This cleared the way for the torture techniques used at Abu Ghraib.<p>
But thanks to the Justice Department's action, Gonzales was able to answer many questions about the infamous torture memo by saying that it had been "repudiated."<p>
Meanwhile, his testimony made it clear that, in the words of the <i>Washington Post, </i>"he will seek no change in practices that have led to the torture and killing of scores of detainees."<p>

