<font face="Times New Roman, Times, serif" size="4">Democrats agree not to filibuster Bush's main nominees</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>When compromise means surrender</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 27, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">WHEN BRITISH antiwar movement leader and newly re-elected member of parliament George Galloway confronted a Senate committee last week over the U.S. invasion of Iraq, opponents of the war around the world were watching--and celebrating.<p>
"Senator, in everything I said about Iraq, I turned out to be right, and you turned out to be wrong," Galloway told committee chair Sen. Norm Coleman (R-Minn.), who the <i>New York Times </i>politely described as "flummoxed." "And 100,000 people paid with their lives--1,600 of them American soldiers sent to their deaths on a pack of lies; 15,000 of them wounded, many of them disabled forever on a pack of lies...[I]f the world had listened to me and the anti-war movement in Britain, we would not be in the disaster that we are in today. Senator, this is the mother of all smokescreens."<p>
Galloway's dressing-down of the Republicans was a joy to see and hear for the millions of people who thirst for a real challenge to George W. Bush and the Republicans. It stood out all the more in comparison the behavior of the official "opposition" to the Republicans in U.S. politics--the Democratic Party.<p>
As <i>Socialist Worker </i>went to press, the Democrats rolled over once again--agreeing to a rotten compromise on Bush's right-wing judicial nominees. <p>
Consigned to a minority in both houses of Congress by last year's dismal election defeat, the Democrats had at least promise to fight the worst of Bush's picks for a lifetime appointment to the federal bench. In turn, the Republicans threatened what they called the "nuclear option"--changing the Senate rules to ban filibusters, under which a senator can hold the floor indefinitely to block proceedings.<p>
After weeks of backroom negotiations, a bipartisan group of senators came up with a compromise under which the Democrats...agreed not to use the filibuster against the worst antigay and anti-abortion bigots among Bush's nominees-- Priscilla Owen, Janice Rogers Brown and William H. Pryor.<p>
In return, the Democrats can still use the filibuster in the future, but only in "extraordinary circumstances." Liberal commentators will probably hail the deal as a hard-won victory, but you can count on the Democratic Senate leadership abandoning future fights because the circumstances aren't "extraordinary" enough. Thus, on CBS's <i>Face the Nation</i>, Sen. Dick Durbin (D-Ill.), considered a leader among <i>liberal</i> senators, vowed, "The Democrats will not shut the Senate down. We will not shut the government down."<p>
On the other hand, Republican wings-nuts like Sen. Rick Santorum (R-Pa.) understand what it means to go on the attack. Last week, Santorum compared the Democrats to Adolph Hitler's Nazis occupying Paris.<p>
Bush and the Republicans are going for broke. Today, it's right-wing judges; yesterday, it was $82 billion more for war and occupation; tomorrow, it will be Social Security and women's right to abortion.<p>
The Democrats aren't an opposition party. While there may wrangle over the details, the Democrats agree with the Republicans on the fundamentals.<p>
Anger at the Bush agenda is simmering just below the surface. Last weekend, it spilled over at the unlikely setting of Calvin College, an evangelical Christian College in the Republican stronghold of Grand Rapids, Mich., where Bush was scheduled to address graduating seniors.<p>
In the days before he got there, more than 800 faculty members, alumni, students and others signed full-page ads in local newspapers condemning the Bush administration. When Bush arrived, he was greeted by protesters carrying signs that read, "No More Blood for Oil."<p>
That shows the potential for building a real opposition to Washington's pro-war, pro-business agenda.

