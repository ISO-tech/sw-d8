<font face="Times New Roman, Times, serif" size="4">An inspiration to those on death row</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Just one more day"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 27, 2005 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">STANLEY HOWARD is a former Illinois death row prisoner who was exonerated and pardoned by Gov. George Ryan in January 2003. He wrote this tribute to William Bracy, another former death row prisoner whose sentence was commuted to life in prison.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">MR. WILLIAM Bracy was indeed a true inspiration. He recently died of cancer due to medical neglect and incompetence, and I was asked to tell the world about this great man and what he meant to me.<p>
Bracy was a tall, slim, light-skinned man in his early 60s, with salt-and-pepper hair, who walked with his head held high and with incredible confidence. He was arrested for a November 12, 1980, murder that occurred in Chicago. <p>
Even though he had a solid alibi as to his whereabouts at the time of the incident (he was at his sister's house), Judge Thomas Maloney refused to allow his sister to testify, claiming "his attorney did not properly put the court on notice of the alibi defense." Bracy testified on his own behalf concerning his innocence, but was still wrongfully convicted and sentenced to death.<p>
I first met this humble, soft-spoken and subdued man when I arrived on death row at Pontiac, Ill., in April 1987, and quickly discovered why all the other prisoners and prison staff treated him with respect and admiration--more so than any other prisoner I've ever come in contact with. He was one of the nicest and most understanding people on earth.<p>
Not only was he a God-fearing man who was well versed in the scriptures, very intelligent and a great painter and artist (who taught a large number of death row prisoners how to draw and paint), but he had a smile and uncanny ability to make anyone feel delighted and comfortable while in his presence. <p>
As an elder statesman on death row, he was a father figure, big brother and true friend to all of us, and he used his brotherly love to inspire all death row prisoners to remain strong and positive while suffering the everyday threat of death and some of the most inhumane conditions in the world. It's because of him that a lot of death row prisoners were able to maintain some sense of sanity without falling completely apart.<p>
After losing my grandmother, who was the love of my life, and having my first appeal denied in the early 1990's, the walls of death row started suffocating the life right out of me, and had me on the verge of a mental and emotional breakdown. Bracy was one of the main guys that held me up throughout the ordeal. He told me that the system wanted me to fall apart in this manner, and that I had an obligation to remain strong for my family.<p>
I will always remember the day I told him that I just couldn't take being on death row anymore. Instead of giving me one of his heart-to-heart talks, he simply asked me to do him a favor by doing "just one more day." At that time, I thought that was one of the craziest things I ever heard, but his solution ("just one more day") has kept me strong for many years by doing one day at a time. That's just the kind of guy he was--always giving sound and memorable advice, helping others and showing that he cared.<p>
Judge Maloney was convicted and sent to prison for accepting bribes to fix cases in the Greylord scandal, and Bracy received a new hearing from the U.S. Supreme Court because it was determined that the excessively harsh treatment he received from Judge Maloney was "to hide or to compensate for the fact that leniency was sold in other cases." The court ruled that Bracy had a right to a "fair trial in a fair tribunal before a judge with no actual bias," and it's unfortunate and unjust that he never received the chance to be rightfully reunited with his family and loved ones.<p>
Bracy is no longer physically with us, but his love, guidance and inspiration will forever shine in our hearts. We love you William Bracy and know that you are safely in God's loving embrace! I send my deepest condolences to his family and loved ones on behalf of all of us, and those who work endlessly in the Campaign to End the Death Penalty.<p>

