<font face="Times New Roman, Times, serif" size="4"><b>Corporation for Political Bias</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THE REPUBLICAN chair of the Corporation for Public Broadcasting says he wants PBS to be more conservative. Without the knowledge of his board, Kenneth Y. Tomlinson contracted last year with an outside consultant--at a cost of $10,000 for three months--to keep track of the guests' political leanings on one program, <i>Now With Bill Moyers</i>.<p>
It's not surprising Tomlinson would pick Moyers' show--as opposed to, say, the conservative <i>John McLaughlin's One on One</i>--to make a point, since liberal journalist Moyers has long been known for his measured critiques of the political system.<p>
But Tomlinson went further. In late March, on the recommendation of Bush administration officials, Tomlinson hired the director of the White House Office of Global Communications as a senior staff member. Tomlinson also encouraged corporation and public broadcasting officials to broadcast <i>The Journal Editorial Report</i>, whose host, Paul Gigot, is editor of the hard-line conservative editorial page of the <i>Wall Street Journal</i>. <p>
Strangely, while Moyers' guests included many conservatives, like Ralph Reed, former head of the Christian Coalition; Richard Viguerie, a conservative political strategist; and Grover Norquist, president of Americans for Tax Reform--Gigot's show by design features only members of the conservative editorial board.<p>
Last November, when members of the Association of Public Television Stations met in Baltimore along with officials from the corporation and PBS, Tomlinson told them they should make sure their programming better reflected the Republican mandate. Later, he said that his comment was in jest and that he couldn't imagine how remarks at "a fun occasion" were taken the wrong way. <br>
-- <b><i>New York Times</i>, </b>May 2, 2005<p>

<font face="Times New Roman, Times, serif" size="4"><b>The collar of money</b></font><p>

<font face="Times New Roman, Times, serif" size="3">HOW CAN dog lovers with money to burn spend their time--and their cash?<i> New York Dog</i>--a new magazine for the well-heeled pooch--is here to help. With stories like "Dogs on Atkins!" "You've got pee-mail!" and "Are mutts the new black?" <i>New York Dog</i> doesn't shy away from the tough issues facing today's pampered dogs and their humans.<p>
Should you buy health insurance for your dog? An article titled "Insure or not so sure?" can help. Wondering if maybe your dog needs a shrink--or just some antidepressants to help take the edge off? Check out "Does your dog need Prozac?" (Yes, if your dog is suffering the loss of a caretaker or "best doggy pal," says the mag.)<p>
And for socialites not smart enough to include their purebred in the pre-nup, the article "Ruff Justice"--featuring an interview with celebrity divorce lawyer Raoul Felder--can tell you how to keep your dog in a custody battle. "Start a diary showing that you are the primary caretaker," is the helpful advice. "Note how many times you walk the dog." <br>
-- <b><i>Washington Post,</i> </b>February 15, 2005<p>

