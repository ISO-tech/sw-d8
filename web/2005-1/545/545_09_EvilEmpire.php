<font face="Times New Roman, Times, serif" size="4">Star Wars rebellion against Bush?</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Evil Empire USA</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Joe Allen</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 27, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Star Wars Episode III: The Revenge of the Sith,</b></i> directed by George Lucas, starring Ewan McGregor, Hayden Christensen and Natalie Portman.</font><p>

<font face="Times New Roman, Times, serif" size="3"><i>STAR WARS Episode III: The Revenge of the Sith </i>opened across the country last weekend to large audiences and generally positive reviews. What makes this episode of <i>Star Wars </i>different from those before it are the open jabs at the Bush administration in the film and Lucas' own attacks on U.S. foreign policy at recent public appearances. <p>
This is Lucas' sixth installment--and what he says will be the last--in the three decades-long Stars Wars saga that began a long time ago, in a galaxy far, far away in the year 1977. Starring Ewan MacGregor as Obi-Wan Kenobi, Hayden Christensen as Anakin Skywalker and Natalie Portman as Padme, the film chronicles the death of the Galactic Republic and the rise of the Empire.<p>
The scheming Senator Palpatine (who has hidden his true identity as an evil Sith Lord Darth Sidious) creates a phony war against the "separatists" in order to give himself "emergency powers." To further his goal for absolute power, Palpatine seduces Anakin Skywalker, a Jedi apprentice with a wounded ego and a lot of gripes, to the dark side of "the Force."<p>
Anakin is to do Palpatine's dirty work in wiping out the last of the Jedi who are pledged to defend the Republic. The last of Jedi flee in the face of the triumph of evil and hide out until the children of Anakin and Padme grow up to lead the rebellion against the Empire in the original, and much better, three episodes.<p>
There are many things wrong with the film--the most obvious being the script, which points to much larger problem with Lucas' filmmaking in general, where writing and character development are almost an afterthought. There was a better balance of this in the original three episodes. It should be kept in mind that what is considered to be the best Star Wars film, <i>The Empire Strikes Back,</i> wasn't directed or written by George Lucas.<p>
But what about the right-wing complaints that Lucas has "politicized" Star Wars by making the final episode an attack on George W. Bush and the Iraq war? In the first five episodes, Lucas avoided talking about any deeper politics involving his Star Wars series.<p>
If anything, it was Republicans who took up Star Wars for themselves. Ronald Reagan called the former Soviet Union the "Evil Empire." John McCain compared himself to Luke Skywalker in his 2000 primary challenge to Bush.<p>
This has changed with <i>Revenge of the Sith, </i>as Lucas hopes his film will have the same impact as Michael Moore's <i>Fahrenheit 9/11. </i>"Maybe the film will waken people to the situation," Lucas is quoted as saying.<p>
He says that he wrote the original script under the influence of the Vietnam War and is opposed to the war in Iraq. "The parallels between what we did in Vietnam and what we are doing in Iraq now are unbelievable."<p>
There are clear references to the Bush administration in <i>Revenge of the Sith. </i>For example, before his final duel with Obi-Won, Anakin declares, "You're either with me...or my enemy," clearly a play on Bush's post-September 11 ultimatum to the world.<p>
And in another scene, one Jedi complains that Palpatine "controls the Senate and the courts." Unfortunately, these and other jabs at Bush come off so clumsy in the movie that they are largely ineffective.<p>
If Lucas hoped that Revenge of the Sith would have the same impact as <i>Fahrenheit 9/11 </i>had on Bush, I doubt the thought of it has even crossed Bush's mind. Where are the Ewoks when you need them?<p>

