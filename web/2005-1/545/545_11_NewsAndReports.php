<font face="Times New Roman, Times, serif" size="4"><b>Shut down the military recruiters</b></font><p>

<font face="Times New Roman, Times, serif" size="3">U.S. MILITARY recruiters across the U.S. held a recruiting "stand-down" May 20--and counter-recruitment activists used the day to draw attention to the dishonest and aggressive tactics that recruiters use.<p>
-- In <b>New York City, </b>more than 80 people demonstrated in front of the U.S. Armed Forces Recruiting Center in the middle of Times Square. "[The recruiting stand-down] shows how the resistance movement is hitting hard at the military," said Ardeshir Ommani, a retired teacher and member of the American-Iranian Friendship Committee.<p>
The demonstration highlighted the success of anti-recruitment activities on campuses and stressed the need to stop all future military recruitment. The event was sponsored by the Campus Antiwar Network, Code Pink, the International Socialist Organization, Not in Our Name, Veterans for Peace and Vietnam Veterans Against the War.<p>
-- In <b>Chicago, </b>the Save Senn Coalition in conjunction with the American Friends Service Committee held a press conference at Senn High School on May 20 attended by 30 people.<p>
The U.S. Navy plans to take over a wing of the school in the fall for a Naval academy. Members of Save Senn joined students to pass out flyers with an "opt-out" form so students could get their names off the list supplied to the military by public schools complying with the No Child Left Behind Act.<p>
The next day, more than 20 people gathered to protest Armed Forces Day at Navy Pier, Illinois' largest tourist attraction. On hand were recruiters using flight simulators, toys, chin-up competitions, free T-shirts and basketball contests to lure young people into giving them their phone numbers.<p>
Despite being harassed by police and recruiters alike, protesters stood their ground and passed out counter-recruitment literature addressing everything from sexual harassment in the military to how few GIs actually receive money for college.<p>
<i>Susan Dwyer, Sarah Macaraeg, Tamar Szmuilowicz contributed to this report</i>.<p>

