<p><a name="Solidarity"></a><font face="Times New Roman, Times, serif" size="4"><b>Philippines solidarity</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Eduardo R.C. Capulong</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--About 40 protesters held a candlelight picket at the Philippine Consulate May 20 to denounce the continued killings of activists in the Philippines. <p>
According to Gabriela Network, the Filipina women's organization that sponsored the event, there have been 32 political killings in the country this year alone. Nearly all of the victims are activists with nationalist organizations such as Gabriela, Bayan, Bayan Muna and Anakbayan. Of these, 13 were journalists, with the Philippines now surpassing Iraq as the "most murderous country" for the media in the world, according to the International Committee for the Protection of Journalists. <p>
Activists blamed the Philippine military and President Gloria Macapagal Arroyo for the killings, saying that they stem from the government's war on any opposition, which masquerades as a "war on terror." "We stand here tonight to call attention to the attacks on all those who refuse to relinquish their right to dissent," said Doris Mendoza, a Gabriela activist. "Continuing the struggle is the only option."<p>

