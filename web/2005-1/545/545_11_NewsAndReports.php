<p><a name="NCAA"></a><font face="Times New Roman, Times, serif" size="4"><b>National Council of Arab Americans</b></font><p>

<font face="Times New Roman, Times, serif" size="3">WASHINGTON--Eighty delegates attended the founding conference of the National Council of Arab Americans (NCA) in Washington, D.C., May 13-15.<p>
The NCA brings together a broad cross-section of Arab activists from different political currents--including Palestinian activists from various political traditions, socialists and representatives of the Muslim American Federation--in a common organization for the first time.<p>
NCA members are focusing on a number of ongoing and proposed national programs, including defense of civil rights in academia, a legal defense and education fund, a history project and a media project. As conference organizers reported, "The conference was unique in every aspect as it gave a real sense of hope for a community yearning for unity and collective empowerment on all levels."<p>
The formation of an organization that gives voice to the opinions and interests of the Arab left can be an urgently needed wake-up call for the U.S. left as a whole. The U.S. antiwar movement has downplayed the victimization of Arabs and Muslims, who have suffered the brunt of the post-September 11 war at home. <p>
One result has been that many liberals within the antiwar movement feel comfortable in veering close to Bush's own rationale for continuing the U.S. occupation of Iraq--that "Islamic extremists" will take over if the U.S. leaves.<p>
NCA members say the organization aims to challenge this. As organizers put it: "From community leaders and sector activists, to academics, students and professionals, the conference was a strong starting point for an organization with a genuine grassroots vision."<p>

