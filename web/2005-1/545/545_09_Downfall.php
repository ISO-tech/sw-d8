<font face="Times New Roman, Times, serif" size="5"><b>A profile of the fall of Nazism</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Jeff Bale</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 27, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Downfall,</b></i> directed by Oliver Hirschbiegel, starring Bruno Ganz, Alexandra Maria Lara and Juliane Kohler.</font><p>

<font face="Times New Roman, Times, serif" size="3">AS THE 60th anniversary of the end of the Second World War approaches, a national debate about the nature of the Nazi regime has re-emerged in Germany. Two new German films approach the question very differently, each highlighting what's at stake in this debate.<p>
One film, a biography of Sophie Scholl--leader of an anti-Nazi student group in Munich--is not out yet in the U.S. The other, <i>Downfall: Hitler and the End of the Third Reich, </i>is here, and was nominated for the 2005 Academy Award's Best Foreign Film.<p>
<i>Downfall </i>is a deeply disturbing and problematic movie--yet one well worth seeing. In the first four weeks of its release, more than three million Germans saw the movie.<p>
The movie--based on two historical accounts--focuses on the last six days of the Nazi regime and takes places almost exclusively in the claustrophobic confines of Hitler's bunker in Berlin. Swiss actor Bruno Ganz breaks new ground in a stunning portrayal of Hitler.<p>
Much criticism has been levied against the film for making Hitler "human" and not simply the raving lunatic that he is usually shown as. But humanizing Hitler is actually important, because it forces us to think about what other factors in society could have produced such a racist tyrant and a regime built on torture, execution and war.<p>
If Hitler wasn't just a crazy old man, then something else must explain the ideology of Nazism and the atrocities they committed. But while Ganz does his part setting the questions up, the rest of the film fails to provide the answers.<p>
The victims of the Nazi regime play virtually no role in this movie at all. The impact of the war on ordinary Germans is also largely left out of this movie.<p>
The last days of the war saw the Allies engaged in wanton destruction of German civilian populations. The devastation is largely unseen in this movie--although it's alluded to in a freaky speech by Hitler's propaganda minister Joseph Goebbels, who says, "The German people have chosen this fate [of war and destruction]. No one forced them into it."<p>
Much of the film is dedicated to portraying the moments when Hitler's inner circle decides when and how they will commit suicide. It would be wrong to conclude, however, as some reviewers have, that <i>Downfall </i>encourages its audience to feel sympathy for Hitler and his inner circle.<p>
Even if the regime's victims are relegated to a supporting role, they are there, and the speeches by Hitler and his henchmen remind us of the atrocities they committed. The most problematic aspect of the film is that it reinforces the most backward explanation for how Hitler came to power.<p>
Up to the last hours of his rule, the movie shows nurses and petty soldiers swearing their fealty to the Nazis. Most problematic is the character of Traudl Junge, one of Hitler's personal secretaries.<p>
In a 2000 interview that opens and closes the movie, she claims to have been wholly unaware of Hitler's actions and the victims his regime produced. She claims, in fact, that until she saw the memorial to Sophie Scholl years later in Munich, she never understood the consequences of her work with the Nazi regime.<p>
This speech underscores the false dichotomy by which the Nazi regime is most often understood: that either <i>all</i> Germans were to blame, or only the most ardent Nazis were--and everyone else was ignorant.<p>
The reality is something different. Many Germans were ardent Nazi supporters up to the end; many others not only knew of Hitler's atrocities but organized to help stop them. That is a much more difficult history to tell, and in leaving it out, <i>Downfall </i>only muddles the debate in German society today about what Hitler and his regime were all about.<p>

