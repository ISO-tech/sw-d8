<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">May 27, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Southern Connecticut State University</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Sam Bernstein and Rich Neagle</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW HAVEN, Conn.--Rank-and-file food service workers in UNITE HERE Local 217 at Southern Connecticut State University (SCSU) held a May 17 rally for fair wages, a just contract and respect on the job. At the rally, about 40 workers and supporters picketed and held a speakout while workers on the clock gave thumbs-up signs through the window of the dining hall. <p>
Chartwells, the mega-corporation that operates the dining halls, wants food service workers' health care co-pays to dramatically increase and to cut seniority benefits for long-time employees. Management also wants to gut union grievance procedures. "In my fifteen years working at Southern, this is by far the worst company we've had to deal with," said Vernon, a rank-and-file leader. "All it wants to do is steal the wealth produced by our hard labor."<p>
The crowd marched into the dining hall in order to collectively confront the manager--who, of course, was not in. <p>
SCSU's food service workers have been working without a contract since February. Meanwhile, a new management team is targeting union activists, imposing discipline over trivial infractions.<p>
Unfortunately, union organizers have failed to prepare or mount an effective campaign. Instead, they tried to pressure workers not to hold a rally, apparently for fear of antagonizing this ruthless company. Angry and frustrated rank-and-file workers, however, planned the rally without union approval.<p>
Although a few union organizers showed up at a planning meeting--the largest union meeting they have had --the workers promptly told them to sit down and raise their hand like everyone else. A union organizer did show up for the rally, and was confronted by a group of 10 workers who wanted to know why the union wouldn't fight harder.<p>
Workers decided to go ahead with their own fighting strategy, formulating plans to protest and disrupt SCSU graduation ceremonies--which the union endorsed only after some hesitation. Rank-and-file activity is key to this fight. As Nick McDonald, a longtime SCSU worker and union activist, put it, "We cannot afford concessions, despite whatever the union heads may say. The only way to win is to strengthen the fight and never give in." <p>

<font face="Times New Roman, Times, serif" size="4"><b>University of Massachusetts-Amherst</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Yuval Sivan, GEO, UAW Local 2322</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AMHERST, Mass.--After negotiating for more than a year, members of the Graduate Employee Organization (GEO) approved a new contract by 111-to-1 May 20. The low turnout reflected the timing of the deal, reached near the end of the semester and ratified at the end of finals week.<p>
The deal is a mixed bag. Individuals will start paying 5 percent of their health care premiums, though families will pay 10 percent (down from 15 percent) and child care will be cheaper. However, the gains in health care and child care are coming from GEO's health and welfare fund, into which the state will pay more. This will give the university more leverage to increase health care costs, even though the contract says they can't do so directly.<p>
On the other hand, grads will get raises of 2 percent a year, with an additional 1 percent a year pool going to increase school and college minimums--especially important since stipends vary greatly across departments. Same-sex domestic partner benefits won't be cut, and the university gave in on many small issues.<p>
With the governor's office pushing health care cuts and 2 percent raises on all state employees, GEO did a little better by comparison. The reason is clear--the contract came after months of rallies and mobilizations, culminating in the April 21 walkout in which thousands of grads and students shut the campus down.<p>
The attacks still aren't over, though--the state legislature refused to fund state contracts before, and there's no guarantee they'll fund ours now. We'll need to join with undergrads and other university employees to beat back the cutbacks.<p>

