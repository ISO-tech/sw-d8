<font face="Times New Roman, Times, serif" size="4">Making kids pay for budget crisis in Seattle</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Why are they closing schools?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Vicky Jambor, SEA, and Jesse Hagopian</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 6, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">OFFICIALS OF the Seattle public school system announced a plan April 20 to close 10 schools and disrupt 14 school programs district-wide to address a projected $20 million budget deficit. <p>
Seattle Public Schools Superintendent Raj Manhas proposed that the district would shut down Bagley, Martin Luther King, Rainier View, Montlake, Genesee Hill, Columbia, John Marshall, Hay School, North Beach and Alki Elementary Schools. In addition, the district's proposal would "re-purpose" other schools--meaning the relocation of successful alternative school programs. The school board will vote on this proposal on July 13.<p>
Another part of the proposed plan would restructure the busing program that currently gives students the chance to go to any school in the city. Under the new plan, students would be restricted to their neighborhood school--effectively, the re-segregation of Seattle schools.<p>
But teachers, parents and students aren't taking this sitting down. Protests and community meetings have taken place across Seattle since the announcement of the proposed closings. <p>
At one meeting at TT Minor, a school slated for "re-purposing," parents, teachers, staff and community members raised their voices against a proposal that they said would negatively affect their children. "They call this 're-purposing,' but what they are really doing is giving us an eviction notice," one parent said of the plan, under which TT Minor that would send all of children, teachers and staff to other elementary schools around the area. "This school doesn't belong to [the school district administrators]," she continued. "It belongs to the people in the neighborhood." <p>
Another parent said, "They built two sports stadiums downtown, but we don't have enough money for our kids to have a good education? It doesn't make any sense."<p>
The district's plan will also mean increased class sizes and possible teacher layoffs. Officials justify the cuts by pointing to the declining number of students attending public schools in Seattle. But in 2000, Seattle voters approved Initiative 728, which called for a reduction in class sizes--not school closings to maintain larger sizes.<p>
Under the Basic Education Act passed by Washington's legislature in 1977, the state bears responsibility for fully funding K-12 education--but the level of funding for public schools has steadily declined ever since, falling below the national average. <p>
Meanwhile, the overall state budget has been reduced by some 500 tax exemptions for corporations, resulting in a net loss of $64 billion in funding for state programs. Washington's shrinking state budget due to tax breaks for corporations has left our schools drastically underfunded. <p>
By the district's own figures, the proposed closings and the restructuring of Seattle Public Schools will only save between $2.6 million the first year and $3.2 million the following year. That's obviously not the solution to a $20 million budget shortfall. To solve the budget crisis, we need to demand that the state fully fund public education. <p>
Unfortunately, the Seattle Education Association has supported the proposed school closures to justify teacher pay raises. The union should remember that Initiative 732 required cost-of-living adjustments for all K-12 school employees, but the legislature suspended I-732 because of a "lack of funds." Instead of hurting teachers, students and community members by supporting school closures, the union should be organizing and fighting for the money that the state owes us.<p>
Even though the union has not organized opposition, parents, students and teachers are getting ready to fight back. A petition has been started to demand that the school board vote "no" on July 13, and plans are in the works for a rally and meetings before the end of the school year.<p>
