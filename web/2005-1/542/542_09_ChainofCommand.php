<font face="Times New Roman, Times, serif" size="5"><b>How they got their war on the world</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Susan Dwyer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 6, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">Seymour Hersh, <b><i>Chain of Command: The Road from 9/11 to Abu Ghraib. </b></i>Harper Collins Publishers, 2004, 416 pages, $25.95.</font><p>

<font face="Times New Roman, Times, serif" size="3">APRIL HAS been an interesting month in U. S. politics. The CIA publicly announced that, having found no weapons of mass destruction in Iraq, they were calling an end to the search.<p>
While the CIA was catching up to common knowledge, the U.S. government was transferring prisoners from Guant&aacute;namo Bay to Afghanistan--some were set free, some were re-imprisoned in Afghan jails. Guant&aacute;namo is now ready for "fact-finding" tours.<p>
Much of our knowledge of the daily torture practiced against the hundreds of people imprisoned in Abu Ghraib is due to a series of <i>New Yorker </i>articles written by Seymour Hersh. His reporting on the torture scandal as well as the Bush administration's political maneuvering in the run-up to the wars in Afghanistan and Iraq have been expanded into his new book, <i>Chain of Command.</i><p>
Hersh is no stranger to Washington's underbelly. He rose to prominence by exposing the political scandal surrounding the My Lai massacre in Vietnam, and the success of Richard Nixon's chain of command in confining the massacre to a single, isolated event pinned on Lt. William Calley.<p>
<i>Chain of Command </i>isn't an exhaustive history of U.S. politics after September 11 but rather a narrative that ties together the political and military policies that created the administration's prisoner policies and that led to the wars in Afghanistan and Iraq.<p>
In a period when the mainstream media seem to have forgotten that the U.S. has troops in Afghanistan and tortures prisoners, Hersh ties both wars and the torture to the Bush administration's ability to lie, twist facts and overturn international treaties as it bullies the rest of the government into backing it up.<p>
It isn't as though long-term analysts and officials at the CIA, the Pentagon, the Department of Defense and the State Department didn't want or know how to wage war; they simply thought that they would be able to control how the wars were justified and carried out.<p>
Thus, Hersh writes, "In the spring of 2002, the Bush administration remained sharply divided about Iraq. "There was widespread agreement that Saddam Hussein should be overthrown, but no agreement about how to get it done."<p>
Bush, Rumsfeld and Cheney concocted a plan. They began a pattern of destabilizing departments by using junior analysts and officials under the closed direction of the White House to go around what they considered to be less hawkish generals and department heads. The CIA began to fall apart as analysts and officials quit in disgust.<p>
Also by 2002, according to Hersh, "[t]he dispute between the Pentagon and the State Department has become even more personal. 'It's the return of the right-wing crazies, crawling their way back,' one of [Deputy Secretary of State Richard] Armitage's associates said, referring to [Deputy Defense Secretary Paul] Wolfowitz's team. "'The knives are out.' The senior State Department official angrily told me that he would 'meet them'--his 'pissant' detractors in the Pentagon--'anytime, anywhere.'"<p>
Little in <i>Chain of Command </i>is new information. We know by now that Tony Blair, Bush, Dick Cheney and Donald Rumsfeld were lying when they "proved" that Iraq had contracted with Niger for 500 tons of uranium. We also know that Rumsfeld's insistence on a small ground force supported by precision bombing caused supply lines to break down, and that using reservists led to untrained troops being forced into situations they couldn't handle.<p>
But Hersh--the consummate reporter--follows these stories through all the twists and turns until an understanding of how and who is possible. It's the details behind these and other stories that make <i>Chain of Command</i> so valuable.<p>

