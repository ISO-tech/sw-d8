<font face="Times New Roman, Times, serif" size="5"><b>U.S. prison population jumps to new high</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 6, 2005 | Page 5</font><p>

<font face="Times New Roman, Times, serif" size="3">THE "LAND of the free" is warehousing its citizens in prisons at shocking rates. According to the latest report from the federal government's Bureau of Justice Statistics, in mid-2004, the nation's prisons held 2.1 million people.<p>
In just a single year, the U.S. prison population grew by 48,000 inmates--a rate of about 900 per week. The U.S. continues to have the highest rate of incarceration in the world--with one of every 138 residents in prison or jail in 2004.<p>
Last year's increase took place even as the crime rate fell. According to the report's co-author Paige Harrison, the number of people going to prison and jail is outpacing the number of inmates released in large part because of mandatory-sentencing laws for drug crimes, "three-strikes-and-you're-out" laws for repeat offenders, and "truth-in-sentencing" laws that restrict early releases.<p>
As Malcolm Young, executive director of the Sentencing Project, told the Associated Press, "We're working under the burden of laws and practices that have developed over 30 years that have focused on punishment and prison as our primary response to crime."<p>
The new statistics show that racism remains an essential part of the race to incarcerate. In 2004, nearly 60 percent of prison and jail inmates were racial or ethnic minorities, and an estimated 12.6 percent of all Black men aged 25 to 29 were in jails or prisons--compared with 3.6 percent of Hispanic men and 1.7 percent of white men in that age group.<p>
And if the Bush administration gets its way, the prison population will continue to rise. Last month, federal marshals--working in conjunction with state and local law enforcement--carried out a weeklong roundup targeting people with outstanding warrants, which produced 10,300 arrests.<p>
"There is no hunting like the hunting of men," Kim Widup, a U.S. marshal for the Northern District of Illinois, told reporters. "And those who have hunted long enough and liked it never care for anything else thereafter."<p>
Federal officials claimed the dragnet was taking "murderers and rapists" off the street. But more than 4,300 of those arrested were wanted for drug crimes, and hundreds more were charged with relatively minor crimes like burglary, probation and parole violations.<p>

