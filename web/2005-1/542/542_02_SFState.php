<font face="Times New Roman, Times, serif" size="5"><b>Activists at SFSU won't be silenced</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Todd Chretien and August Beck</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 6, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">OFFICIALS AT San Francisco State University are threatening three students and two student groups with disciplinary action for their part in a protest against military recruiters. But students aren't taking this attack on our right to protest lying down.<p>
The controversy stems from a March 9 demonstration, where 150 students protested the presence of recruiters on campus. After a peaceful three-hour sit-in and speakout around the recruiters' tables, the military packed up and left early. <p>
Campus officials want to single out a few students and two organizations--Students Against War (SAW) and the International Socialist Organization--as a way of intimidating further activism. But on April 27, supporters of those facing discipline hosted a press conference, heavily covered by the mainstream media, and a public forum--to tell the other side of the story.<p>
One of the featured speakers was veteran civil rights lawyer Lynne Stewart, who was recently convicted on trumped-up charges of "abetting terrorism" and will be sentenced in September. "We all looked to SF State in the 1960s as a beacon in the civil rights movement," Stewart said, "and by you standing up against the war and for your rights here today, people across the country will again look to your school." <p>
As Matt Gonzalez, a leading Green Party member and former president of the San Francisco Board of Supervisors said, "They convicted Lynne Stewart out of fear, not based on facts. The worst thing is to be quiet at a time like this. Even if they convict you or prosecute you, you have to speak out and teach people the truth." <p>
Cindy Sheehan, a cofounder of Gold Star Families for Peace and member of Military Families Speak Out, whose son Casey was killed in Iraq last April, also raised her voice in defense of activism at SF State. "If Bush thinks this is a just war, he should send his two little party-animal girls to fight it," Sheehan said. "Otherwise, bring all our kids home now! You students can always count on me to help out in any way I can. I only live an hour away, and I will speak out for you anytime. Just give me a floor to sleep on, and I'll be there."<p>
SAW and the ISO are planning future actions. Next week, they will publish a full-page ad in the campus newspaper, demanding that the charges be dropped, the university implement its anti-discrimination policy prohibiting antigay employers from recruiting on the campus; and host a debate about whether recruiters should be allowed on campus, with representatives from the military, the university administration and SAW. <p>
