<font face="Times New Roman, Times, serif" size="5"><b>A new Social Security shell game</b></font> <p>
<font face="Arial, Helvetica, sans-serif" size="2">May 6, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">AFTER USING his presidency to steal from working people and give to the rich, George Bush has a new plan for the Social Security system: steal a lot more from workers while taking a teeny bit from the rich. That's the essence of Bush's proposal on "progressive price indexing" of Social Security.<p>
With his effort to sell Social Security private accounts bogged down, Bush used his April 28 press conference to promote "progressive price indexing" as a way to make his scheme seem more equitable.<p>
Under indexing, low-paid workers would continue to get benefits based on 49 percent of their pre-retirement wages--meaning that they wouldn't suffer from cuts if they didn't participate in the private accounts proposed by Bush. <p>
Most workers, however, would face cuts. According to calculations by the Center on Budget and Policy Priorities, workers making average pay--currently $37,000--would face a benefit cut of 10 percent of pre-retirement income by 2075. Meanwhile, the super-rich would see a much smaller benefit cut as a percentage of their pre-retirement income--just 1 percent for someone whose pay is the equivalent of $1 million in 2005 dollars, according to <i>New York Times </i>columnist Paul Krugman.<p>
Economist Dean Baker, co-author of the book <i>Social Security: The Phony Crisis, </i>made similar calculations. "The reason that the resulting benefit cut is regressive is that middle-income workers are far more dependent on income from Social Security in retirement than are higher-income workers," because they have other sources of income, Baker wrote in a paper posted on the Center for Economic and Policy Research Web site. "This means that even though the benefits for the highest-income workers are cut by the most in percentage terms, middle-income retirees will actually see the largest percentage loss in retirement income as a result of the proposed benefit cuts."<p>
In fact, the basic dynamics of the Bush plan haven't changed. Faced with certain cuts in benefits, workers will be compelled to divert some of their payroll taxes into private accounts, thereby limiting the amount received in Social Security benefits in the future.<p>
Income from those private accounts may or may not make up the difference in benefit cuts, depending on the performance of financial markets and investment decisions. For Wall Street, however, private accounts are a sure moneymaker, as investment banks skim off big management fees.<p>
There's also a loophole in the Bush plan that apparently will allow the highest earners to avoid repaying money diverted from the payroll tax into private accounts. In theory, this money is to be treated as a loan, repaid through deductions from expected Social Security benefits. For top earners, however, the amount in their private retirement accounts would be far greater than their expected government benefits--and there's no way for the government to collect the amount that these wealthy people would owe back to the system.<p>
Bush is betting that complex accounting will hide all this--and that sunny hype will revive his plan for private accounts. Now is the time for retirees, organized labor and others in the emerging coalition to save Social Security to put Bush on the run and demand what we need: an increase in benefits and real retirement security.<p>

