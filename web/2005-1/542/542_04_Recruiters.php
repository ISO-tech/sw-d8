<font face="Times New Roman, Times, serif" size="5"><b>Two recruiters on the hot seat</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">May 6, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">AFTER WORK last week, I received an interesting phone call from two Army National Guard recruiters in Pennsylvania. They got my phone number off an advertisement in <i>Socialist Worker</i> for a counter-recruitment event at my school featuring Nadia McCaffrey, a Gold Star mother--the mother of a soldier killed in Iraq.<p>
For about 45 minutes, they peppered me with questions about why I was trying to make their jobs harder. The first sergeant I spoke with was cordial, but kept emphasizing that our activities were hurting his recruitment. He admitted, "Sure, we embellish a little," but contested that the military ever told actual lies.<p>
After I was through with him, he called in his buddy, who was much more aggressive. At one point, the second recruiter told me: "You're anti-establishment, the military is the establishment. The military is made of people. You're anti-people!" I think that this is the first time, as a socialist, I've been accused of being "anti-people."<p>
Why was this man so upset with me? He admitted that because of antiwar activists, no one wants to join the military any more out of "patriotism," so they have to entice people with $10,000 signing bonuses. Of course, he denied that this targeted the poor.<p>
Although I didn't make much headway with either of these two gentlemen in our one conversation, it's nice to know that what we do matters. It matters that two military recruiters spent part of their evening trying to talk me out of politics--rather than working on conning the local high schoolers into making the worst mistake of their lives.<br>
<b>John Green, </b>Davis, Calif.

