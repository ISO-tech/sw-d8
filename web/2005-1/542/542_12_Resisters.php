Likewise, Sgt. Kevin Benderman--who applied for conscientious objector status before his unit was scheduled to redeploy to Iraq late last year--learned last week that the Army had rejected his application, setting the stage for his May 12 court-martial trial at Fort Stewart in Georgia. <p>
Paredes faces up to one year behind bars, a bad-conduct discharge, forfeiture of pay and a reduction in rank--Benderman could be jailed for up to seven years and given a dishonorable discharge. <p>
The military is throwing the book at Paredes and Benderman in the hope of silencing them and squelching further dissent in the ranks. Paredes has been a constant campaigner against the war since he appeared at the 32nd Street Naval Station in San Diego December 6--where his ship was scheduled to leave from--wearing a T-shirt that read, "Like a cabinet member, I resign!" <p>
At the impromptu press conference he organized that day, Paredes pointed out that he would have been safely removed from almost all dangers of the war--but that he didn't want to be an accomplice in the occupation of Iraq, which his ship's mission was aiding. "The fact is that on December 6th, when this all started...there were only two options," Pablo says. "One was right and one was wrong, and I have no regrets." <p>
Since then, Paredes has spoken at antiwar rallies and forums--even though he is confined to the naval base during the day--with a confidence that disarmed his most hostile pro-war detractors. He has attracted support from opponents of the war across the country, including Martin Sheen, Howard Zinn and many others. Now, he recently told his supporters, "it's the final stretch, and it's time to bang the pots really loud." <p>
Paredes and his supporters are calling for solidarity actions to take place the week of his court-martial. Kevin's wife, Monica Benderman, and Pablo's brother, Victor Paredes, are among those calling for a national day of action for GI resisters on May 10, before the two trials start.<p>
In San Diego, a support committee has a series of events planned, with forums that will include activists such as author Naomi Klein and fellow GI resisters Camilo Mej&iacute;a and Aidan Delgado. Activists are also planning a mock trial outside the naval base where the court-martial will be held to "Put the Iraq War on trial, not a war resister!"<p>
Meanwhile, Kevin Benderman has also made his views clear--despite the Army's intimidation tactics, including a ban on his travel that prevented him from traveling to speak at several antiwar forums.<p>
Benderman, a mechanic with 10 years in the Army, spent eight months in Iraq at the start of the war in 2003. He said that witnessing the terrible consequences for Iraqis--including the sight of a badly burned young girl, and mass graves filled with men, women and children--convinced him to apply for conscientious objector status, rather than redeploy to Iraq with his unit.<p>
Now, Army officials have unexpectedly rejected the application ahead of next week's court-martial. "I'm prepared to follow through on my beliefs," Benderman said in an interview with <i>Socialist Worker </i>earlier this year. "I have to be able to accept whatever consequences come from my actions."<p>
When soldiers and sailors stand up and refuse to kill or be fodder for this war, it's a crucial step toward stopping the U.S. war on the Iraqi people. We need to build all-out support for heroes like Pablo Paredes and Kevin Benderman.<p>

