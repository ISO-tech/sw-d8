<font face="Times New Roman, Times, serif" size="5"><b>Put on trial for refusing to go to war</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Justin Akers</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 6, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">PABLO PAREDES will go on trial May 11 at a special court-martial--for refusing to take part in Washington's war for oil and empire in Iraq. But antiwar activists in San Diego--where last December Paredes refused to board his Navy ship, the USS Bonhomme Richard, bound for the Persian Gulf with 3,000 Marines--and around the country are rallying to his defense.<p>

