<font face="Times New Roman, Times, serif" size="4">The right to choose abortion is under attack...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>We won't go back!</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 6, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE RIGHT wing is out to further restrict a woman's right to choose abortion--and Democratic leaders who claim to be pro-choice are surrendering even more ground.<p>
Last week, the House passed the Child Interstate Abortion Notification Act, which would apply criminal penalties to adults who accompany women younger than 18 to get an abortion across state lines without the consent of their parents. The law would also impose a 24-hour waiting period for young women who travel to another state for an abortion (in some cases, even if they are accompanied by their parents)--and force physicians to notify a young woman's parents of her abortion in person, in other cases.<p>
The legislation, which will now be taken up by the Senate, represents another blow to women's already shrinking right to choose. <p>
Already, 33 states have some form of parental notification law. But even in states where there are no such laws, government officials are more than willing to tell young women what to do with their bodies.<p>
In Florida, for example, the state's social services agency went to court last week to block a 13-year-old from having an abortion. The girl, who lives in a state shelter, notified her caseworker that she wanted an abortion, and even received counseling at a women's health clinic. As she reportedly said at a court hearing, "I don't think I should have the baby because I'm 13, I'm in a shelter, and I can't get a job." <p>
The Bush administration and its Christian Right supporters have had their sites trained on overturning abortion rights since the day they took over the White House. Now, in Bush's second term, a number of measures that would whittle away at the right to choose by extending legal rights to fetuses--like the "Unborn Child Pain Awareness Act," which would require doctors to offer an anesthetic for fetuses of women seeking abortions after 20 weeks of pregnancy--are up for consideration.<p>
The right wing is on the offensive against women's reproductive freedom overall. At least 12 states have introduced "conscience clause" laws, which allow pharmacists to legally refuse women access to the birth control option known as the "morning-after pill"--including victims of rape and sexual assault, or women whose birth control methods failed. The Christian Right group "Pharmacists for Life" is spearheading the drive to deny women access to the morning-after pill.<p>
Yet while the right has led the war on abortion rights, Democrats are going along for the ride.<p>
Fifty-four House Democrats voted in favor of the Child Interstate Abortion Notification Act--giving the Republicans a comfortable margin of victory. Democratic National Committee chair Howard Dean last month told <i>USA Today</i> that the party should encourage more "pro-life Democrats" to run for office.<p>
And recently, Sen. John Kerry (D-Mass.) joined forces with ultra-conservative Sen. Rick Santorum (R-Pa.) to introduce the "Workplace Religious Freedom Act"--which contains a national version of the "conscience clause" laws which, according to the <i>New York Times</i>, "would allow a pharmacist to refuse to dispense certain drugs as long as another pharmacist on duty would." <p>
Legislation like this--co-sponsored by their Democratic "ally"--ought to outrage pro-choice organizations. Yet because they are so firmly tied to the coattails of the Democratic Party, mainstream pro-choice organizations have remained all but silent in the face of these betrayals.<p>
A woman's right to abortion and reproductive freedom is essential to the struggle for women to be equal in this society. We have to rebuild the fight for abortion rights at the grassroots--one that doesn't depend on lobbying Democrats or concede to someone's version of "moral values," but that takes an unapologetic stand in defense of every woman's right to control her own body.<p>
We need to confront anti-choice forces wherever they decide to show themselves--whether that means picketing pharmacies that deny women the morning-after pill, or protesting lawmakers, Democrat or Republican, who want to make it harder for women to exercise their choice. It's time to draw the line--we won't go back to the days of back-alley abortions.<p>

