<font face="Times New Roman, Times, serif" size="4"><b>Fight for immigrant rights</b></font><p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES--Nearly 1,300 people marched through the notorious garment sweatshop district on April 30 in support of the rights of workers and immigrants. The International Workers Day march was organized by a diverse group representing immigrants, students, workers and labor unions known as the Multi-Ethnic Immigrant Workers Organization Network.<p>
In Spanish and English, protesters chanted against the war and for labor rights. "Queremos trabajos, queremos escuelas, queremos hospitales--no queremos militares! (We want jobs, schools and hospitals, not war)" was a favorite. Bystanders lining the march shouted encouragement and support. <p>
At the end of the march, several speakers expressed their anger at Gov. Arnold Schwarzenegger, who recently spoke out in support of the Minutemen, the anti-immigrant vigilantes who want to replicate their armed patrols of Arizona's border in California this June.<p>
Domestic worker Guadalupe Lopez spoke of long hours without breaks. It's time "for unity among all immigrants to demand rights now," she told the crowd.<p>
-- In <b>Greenbelt, Md., </b>a crowd of 1,000 made up largely of Latino residents of Maryland held signs and chanted outside the Greenbelt Motor Vehicle Administration on April 30. Members of CASA of Maryland, Inc. organized the protest to put a spotlight on Maryland's efforts to deny driver's licenses, regardless of their legal status in the U.S. <p>
Racist groups opposed to immigrants, like the Federation of American Immigration Reform, also staged a counter-protest. But the crowd met their presence with lively chants and powerful speakers.<p>
"Immigrants put more than $7 billion into saving Social Security every year, and these people are coming here to work hard every day," said Gustavo Torres, executive director of CASA, after leading the crowd in chants. Participants chanted a variety of different sayings including, "&iexcl;Bush, escucha, est&aacute;mos en la lucha! (Bush, listen, we're fighting back!)"<p>
<i>Anthony Ali and Robert D. Skeels contributed to this report</i>.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Stop the neo-Nazis</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jeffrey Offermann and Alpana Mehta</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BOSTON--Activists are preparing to confront the neo-Nazi group "White Revolution" on May 8 when the group plans to protest a Holocaust Memorial service at Fanueil Hall. The white supremacist group wants to spread its anti-Semitic message that the Holocaust never happened--but we won't let them go unopposed.<p>
Recently, the National Alliance--a group that expelled White Revolution's chairman Billy Roper after an internal feud--has posted disgusting flyers announcing their presence. Now, this rally will aim to win "White Revolution" an even higher public profile.<p>
History has shown that if fascists go unopposed, they can gain a foothold and recruit others to their views. This in turn allows the "official" right wing to push forward their agenda and makes far-right and fascist views even more accessible.  <p>
We can stop this cycle by coming out in large numbers to keep the Nazis from recruiting a base of support here. In 1994, the last time Nazis tried to hold a demonstration here, hundreds of activists ran them out of town--and they haven't been back for 11 years. We have to show them that they are still unwelcome.<p>
