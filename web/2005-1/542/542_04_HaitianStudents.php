<font face="Times New Roman, Times, serif" size="4">Haitian students face racism in NYC</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Treated like animals</b></font> <p>
<font face="Arial, Helvetica, sans-serif" size="2">May 6, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">ON MARCH 16, 2005, Nancy Miller, an assistant principal at PS34 in Queens, treated Haitian students like animals. <p>
After two fourth-grade Haitian students were involved in a minor scuffle, Miller forced the two students--plus 11 other Haitian students--to sit on the floor and eat with their hands in front of the whole student cafeteria. Miller boldly proved her racist and narrow-minded character when she told the students, "In Haiti, they treat you like animals, and I will treat you the same way." Miller and Pauline Shakespeare, the principal of PS34, then tried to bribe the victims to change their story by offering them ice cream.<p>
One child said afterwards, "Mrs. Miller made me and our classmates sit on the floor to eat our lunch. She said that we are animals and we got it from our country. I was hurt, and when I got to my class I told my teacher about what happened. I did not like what she said about my country."<p>
Though parents of the students protested in front of the school, and there has been an outpouring of anger, city officials still have not fully responded to the incident. The parents demanded that Miller be fired. Instead, she has been reassigned to a desk job at the district office--because she claimed that she "feared for her safety." This is bogus. It was the students' safety and humanity that were violated.  <p>
Mayor Michael Bloomberg finally made a statement that partially acknowledged what occurred. "What apparently happened, if it's true, is despicable and disgraceful and an outrage," he said.<p>
This is not enough. Miller and Shakespeare's behavior was racist, outrageous and shameful. They should both be removed from their posts immediately and from the New York City school system entirely because children don't deserve to be subjected to such bigotry. <p>
These students were brutalized, subjected to collective punishment and public humiliation. Their only crime was that they were Haitians.<br>
<b>Nadjhia Normil and Sarah Hines, </b>New York City<p>

