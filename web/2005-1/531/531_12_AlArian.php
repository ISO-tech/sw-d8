<font face="Times New Roman, Times, serif" size="5"><b>Nightmare behind bars for Al-Arian</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 18, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">LAST MONTH, Dr. Sami Al-Arian spent his 47th birthday in solitary confinement in a Florida prison cell. Al-Arian, a former University of South Florida professor and political activist, has been behind bars for two years, charged with aiding supposed "terrorists."<p>
In 2003, Al-Arian was arrested--along with Sameeh Hammoudeh, Hatem Fariz and Ghassan Ballut--and accused of using an academic think tank, a Muslim school and a charity as a cover for raising money for the Palestinian Islamic Jihad (PIJ), a group that the U.S. State Department has labeled a "terrorist organization." <p>
In reality, Al-Arian and the others are victims of a witch-hunt against supporters of the right of Palestinians to resist Israel's occupation. Many of the most serious charges against Al-Arian and the others stem from their alleged support of the Islamic Jihad <i>before</i> the U.S. classified it a "terrorist group."<p>
Al-Arian's time in prison has been a nightmare. Under "normal" conditions at the Coleman Federal Correctional Complex--where he was housed until recently--Al-Arian's access to his attorneys and visits with his family were restricted. He was confined to his cell for 23 hours each day.<p>
According to activists in the Tampa Bay Coalition for Justice and Peace, which has worked to free Al-Arian, things got worse recently when Coleman officials instituted a "lockdown"--the second in a month's time. The entire prison was closed to visitors--including Al-Arian's lawyers and family--and he was kept confined to his cell for a full 24 hours a day. Laundry service was suspended, and regular food service was also disrupted.<p>
According to the Tampa Bay Coalition, in a span of 46 days, Al-Arian, who is a diabetic, was only given medical attention twice.<p>
In early January, a federal judge ordered Al-Arian to be moved to another facility because of the harsh conditions at Coleman. It took more than two weeks before he was transferred.<p>
"Almost two years since his unjust arrest, our father has not yet been tried and continues to suffer under the most atrocious and inhumane conditions of confinement," said his family in a statement. "The past 694 days have been a trying and traumatic experience, in which countless holidays, birthdays, and anniversaries were spent without our beloved father, with whom we have had only four contact visits."<p>
Still, Al-Arian's family asked that people remember his birthday by remembering the thousands of Palestinians killed under Israel's occupation. As they wrote, "A Palestinian refugee, our father appreciated the freedom of expression that our country offered and felt a profound responsibility to give voice to the oppressed Palestinian people suffering under a brutal occupation."<p>

