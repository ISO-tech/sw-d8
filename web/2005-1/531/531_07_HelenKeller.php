<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The hidden history they won't teach</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 18, 2005 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">WHILE I was drinking coffee in a bookstore yesterday, I flipped through a book for kids about Helen Keller. Predictably, it was a story about a little girl who overcame extreme adversity with the help of "miracle worker" Annie Sullivan--a "little engine that could" story that so warms the hearts of the powers that be, with the message: Try hard and you can make it, too.<p>
The book made not a single reference to the fact that between 1909 and 1921, Helen Keller was a flaming antiwar radical and socialist who wrote and spoke extensively about her views.<p>
What attracted Helen Keller to socialism? Her deep desire for a society free of degredation, exploitation and conquest.<p>
"I have visited sweatshops, factories, crowded slums of New York and Washington," she wrote in 1913. "Of course, I could not see the squalor; but if I could not see it, I could smell it." <p>
She continued: "When we inquire why things are as they are, the answer is the foundation of society is laid upon a basis of individualism, conquest and exploitation, with a total disregard of the good of the whole. The structure of a society built upon such wrong basic principles is bound to retard the development of all men, even the most successful ones, because it tends to divert man's energies into useless channels and to degrade his character. <p>
"The result is a false standard of values. Trade and material prosperity are held to be the main objects of pursuit and conquest; the lowest instincts in human nature--love of gain, cunning and selfishness--are fostered. The output of a cotton mill or a coal mine is considered of greater importance than the production of healthy, happy-hearted, free human beings."<p>
Alarmed at Keller's newfound radicalism, the mainstream press insisted that the deaf, dumb and blind woman had been manipulated and duped.<p>
Keller responded to her detractors in a 1916 speech at Carnegie Hall in New York: "Some people are grieved because they imagine I am in the hands of unscrupulous persons who lead me astray and persuade me to espouse unpopular causes and make me the mouthpiece of their propaganda. Now let it be understood once and for all that I do not want their pity; I would not change places with one of them. I know what I am talking about."<p>
She went on to argue that the U.S. was not entering the First World War to "defend the country," but rather to "protect the capital of American speculators and investors in Mexico, South America, China and the Philippine Islands."<p>
"Every modern war," she said, "has had its root in exploitation...The Spanish-American War decided that the United States should exploit Cuba and the Philippines...The present war is to decide who shall exploit the Balkans, Turkey, Persia, Egypt, India, China, Africa. And we are whetting our sword to scare the victors into sharing the spoils with us. Now, the workers are not interested in the spoils; they will not get any of them anyway."<p>
Keller ended her speech by appealing to workers not to heed the jingoistic propaganda aimed at getting them to support the war, but instead to take strike action against the war. "Strike against war," she said. "For without you, no battles can be fought...Be not dumb, obedient slaves in an army of destruction. Be heroes in an army of construction."<p>
After Eugene Debs was sentenced to prison for opposition to the war, Keller wrote a letter to Debs saying, "I should be proud if the Supreme Court convicted me of abhorring war, and doing all in my power to oppose it. Law and order! What oceans of blood and tears are shed in their name! I have come to loathe traditions and institutions that take away the rights of the poor and protect the wicked against judgment."<p>
This Helen Keller--the passionate fighter for justice--is the one most worth remembering.<p>

