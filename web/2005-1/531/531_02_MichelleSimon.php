<font face="Times New Roman, Times, serif" size="4">Relative of a victim of LA sheriff's officers speaks out</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"The police are totally out of control"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 18, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">LATE LAST MONTH, 22-year-old Kazi Bell was shot and killed by LA county sheriff's officers. Kazi, who leaves behind a 2-year-old son, was just one of three people killed by law enforcement officers in three separate incidents in LA that night.<p>
Kazi's cousin MICHELLE SIMON, a member of the Campaign to End the Death Penalty and the International Socialist Organization in the Bay Area, talked to <i>Socialist Worker </i>about her cousin's death.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>WHAT DO you think of the LA police?</b> <p>

I THINK that they're totally out of control. The same night my cousin was murdered, there were three other shootings in that same area, and I think two other people died. There was a blurb about him in the newspaper only because of the other shootings that happened the same night.<p>
This is really sad. If he were white, I don't think he would have been shot. I think that he and his friends were most likely targeted because they were Black. The news report didn't say why he was pulled over, or that they were doing anything wrong, so I believe that, in some respect, they were being harassed.<p>
Then there are all these things that have happened in the last two or three weeks with people getting shot unnecessarily by police officers. <p>
We have a relative who's a police officer in Houston, and he even thinks that there's something not quite right about what happened to Kazi. He pointed out that the LAPD and the county sheriffs shoot to kill. They don't shoot to wound you, or just as a warning. If they're going to shoot you, they're going to kill you. And if they don't kill you, it's an accident.<p>
<b>HOW DID your family find out about Kazi's murder?</b> <p>
THE SHERIFF didn't call his mother at first. He did eventually call her, but only because she worked for a councilman, who later told the sheriff to call her. I would think that if they shoot and kill somebody, that they would have the decency to try and find out who he is, and then call his family to tell them what happened.<p>
The friend that was with him was thrown in jail. They claimed that they just wanted to talk to him, but this poor kid was so afraid that the cops were going to come after him.<p>

<b>WHAT DO you think needs to be done?</b> <p>

PEOPLE SHOULD make the cops accountable for their actions. All too often, stuff like this happens, and it seems like the cops get to go on their merry way. The cops are there to protect rich people.<p>
I think it's hard for young African-American men to find decent jobs that are going to take care of themselves and their families. You make money whatever way you can. They might turn to selling drugs because it's the only way to make a living to support themselves. I feel that if there were adequate jobs and education for people, this kind of stuff wouldn't happen.<p>
In the LA newspaper, they were interviewing people in the community where the 13 year old was shot. Most people were outraged, but then some people said, "Why was he out at 4 a.m.? Where were his parents? Where was his mother?"--trying to blame the victim and his family for the fact that he was shot and killed by the police. <p>
This is a 13-year-old kid who was unarmed. If he were a little white kid, they wouldn't have shot him--and there wouldn't be so much criticism of him and his family.<p>

