<p><a name="Iraqis"></a><font face="Times New Roman, Times, serif" size="4"><b>What about injustice to Iraqis?</b></font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker</i>,</b> <br>
I totally empathize with the Williams family and what happened to their son due to his stint in Iraq ("'The war changed who my son is,'" February 11). However, I think a major part of the total tragedy has been left out here. <p>
Specifically, why did Rev. Williams and his wife, as well as their son, feel it acceptable for him to go to Iraq and participate in this war? The entire Williams family seems to have been very supportive of a totally unjustifiable war--otherwise, why would they and he not protest against his going to Iraq? Why would he go, and why would they allow him to go? <p>
Very much like the Vietnam War, the U.S. public seems to become cognizant of the evil of a war only when it directly affects them. Meanwhile, the injustice done to the Iraqi people is nothing but a side issue to American casualties of this war. <p>
What happened to the Williams' son is a tragedy, but a tragedy that could have been averted had he and his parents acknowledged and respected the rights of the people of Iraq. The bigger picture is not even perceived here. Why?<br>
<b>Larry Schmidt,</b> Toronto, Canada<p>

