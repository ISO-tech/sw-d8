<font face="Times New Roman, Times, serif" size="4"><b>Standing up to the racists at SFSU</b></font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
There is no room for racists on college campuses. <p>
Recently, at San Francisco State University (SFSU), the College Republicans hosted a speaking event featuring well-known racist, sexist and anti-gay bigot David Horowitz. Horowitz spoke about his organization Students for Academic Freedom, claiming to be a champion of free speech. But in truth, he represents the opposite. <p>
Students for Academic Freedom actually targets students and teachers who speak out against racial profiling and the occupations in Iraq and Palestine. At SFSU, Horowitz spent most of his time distorting the truth and spewing racist lies, including one where he claimed that it was the work of a few great white men that really ended slavery in the United States.<p>
The reaction to Horowitz was less than friendly. No more than half of the audience applauded at what he had to say, and the rest of us hissed and booed him down. Even the faculty who were working the sound system were shaking their heads and hissing. Student groups, including Students Against War, the General Union of Palestinian Students, the International Socialist Organization and others held signs and banners reading, "Horowitz is a Racist" and "End the Occupation Now." The banners showed our disgust with Horowitz and gave others in the room enough confidence to verbally express their own anger. <p>
Unfortunately, even with a clear majority on our side, we could not shut the bigot down. Those who came to protest Horowitz thought it would be playing into his hands to be too disruptive. Others believed that even Horowitz, who makes it a career to silence the left, should have his rights to free of speech. <p>
But hate speech is not free speech. Horowitz is not for academic freedom. He wants to shut up any and all dissent on college campuses. <p>
Horowitz's organization is going on a rampant witch-hunt to silence any opposition to the right. Remaining silent as he does this will only make it easier for him to get away with his attacks on left opposition. Students and teachers must kick racists like David Horowitz off our campuses and show them that they are not welcome.<br>
<b>Sarah Levine,</b> San Francisco<p>
