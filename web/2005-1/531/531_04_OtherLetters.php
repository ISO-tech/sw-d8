<p><a name="Fair"></a><font face="Times New Roman, Times, serif" size="4"><b>Ornelas deserves a fair trial</b></font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
On March 3, Cook County Judge Nicholas Ford will announce a decision on whether or not to give police torture victim Robert Ornelas an evidentiary hearing which could possibly lead to the first new trial for a non-death row police torture victim in Illinois. The decision could be monumental in the fight to win justice for the victims of torture by Chicago police under the command of former Lt. Jon Burge.<p>
Illinois Attorney General Lisa Madigan is in charge of the 118 known Burge police torture cases, after Cook County State's Attorney Dick Devine--who was an assistant prosecutor when the torture was taking place--was removed from the case because of his conflict of interest. <p>
Madigan has already successfully shut down the torture claim of former death row inmate and Death Row 10 member Victor "Cortez" Brown, even though there has been sufficient evidence that the torture occurred. We ask that Lisa Madigan do the right thing and allow Robert Ornelas to at the least have a hearing.<p>
Ornelas was arrested on November 15, 1990, in Frankfurt, Ill., along with three other suspects. The Chicago Police Department was notified, and Detective Yucitis--who has been connected to 10 known torture cases--came for the interrogation. He beat and tortured Ornelas into signing a false confession. Robert told his mother, Mary Ornelas, "If the cops had beat you or anyone else the way they beat me, you'd confess, too."<p>
"There were no real witnesses," said Mary Ornelas. "There was no weapon. The police would not use statements from Robert's alibi witnesses because they had a record, but they used testimonies from ex cons to convict."<p>
Judge Nicolas Ford is mulling over the technicality that Robert Ornelas was beaten outside of the "ring" of Areas 2 and 3, where Burge's torturers operated--despite the fact that Yucitis has been acknowledged as one of the main "players" involved in the scandal. <p>
We ask that everyone show up and be counted at Ornelas' court date on March 3, to bear witness that justice must be served, and to support Mary Ornelas. Stand up and be counted. Enough is enough!<br>
<b>Kenosha Washington,</b> Enough Is Enough! Campaign Against Police Torture, Chicago<p>
