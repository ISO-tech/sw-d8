<font face="Times New Roman, Times, serif" size="5"><b>We can't trust Democrats to protect choice</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 18, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
I'm glad that <i>Socialist Worker </i>ran articles recently on "Have the Democrats surrendered on abortion rights?" and "How to defend the right to choose" (February 4).<p>
On the 32nd anniversary of <i>Roe v. Wade</i>, I was disgusted to see thousands of anti-abortion bigots descend upon Washington, D.C., for a protest aimed at eliminating abortion rights in this country. The event included all the worst elements of the fanatical, sexist right wing--and was addressed by President Bush (via telephone) who promised that their "movement will not fail." <p>
The rest of us had to endure images of the thousands-strong march and the sickening photos of women carrying huge, black and white signs proclaiming, "I regret my abortion," which were splashed across newspaper pages and ran constantly on the evening news. <p>
When activists looked around for ways to demonstrate their support for abortion rights, there was nothing to be found on a national scale. In D.C., the only publicized event was a single clinic defense--an important action, but one that fell short of being any kind of real mobilization and was inadequate for the anniversary of <i>Roe</i>. <p>
So what exactly was it that was keeping the likes of the pro-choice group NARAL Pro-Choice America so busy that they couldn't call a demonstration? I found out just a day later, when I received the first of several e-mails from NARAL imploring me to help "keep the Democratic Party pro-choice." <p>
Instead of mounting any kind of message--much less pressure--to defend <i>Roe v. Wade</i>, NARAL has been busy playing insider politics in the race for chairman of the Democratic National Committee, lobbying behind the scenes for the election of Howard Dean as DNC chair over anti-abortion former Congressman Tim Roemer. <p>
Howard Dean is pro-choice. Many will interpret his ascendancy to the leadership of the DNC as a victory for abortion rights activists. <p>
But consider what Dean has to say in his official statement on reproductive rights: "Throughout my entire professional career, as a doctor, politician, and former Planned Parenthood of Northern New England board member, I have worked hard with allies in the women's community. Together, we have achieved results on issues of critical importance to women, including reproductive rights. Today, we need to be committed to providing women with the health care and education that they need, like family planning and comprehensive sexuality education. And I support the measures included in Senator [Harry] Reid's 'Putting Prevention First Act.'"<p>
In Vermont, where Dean was once governor, 43 percent of all counties lack an abortion provider. Rhode Island is one of the worst states to live in if you are a woman seeking an abortion. Massachusetts has a slew of restrictions on abortion rights on the books. So much for Dean's leadership in New England.<p>
And Howard Dean might think that women need more "education" on "family planning" and "comprehensive sexuality," but I think Dean should be the one to get an education--on how it is sexist to imply that women are too stupid to not get themselves pregnant (as if women manage that by themselves). <p>
Dean's position is as much of a retreat as Sen. Hillary Clinton's recent promise to find "common ground" with the right on this issue. This is clear from Dean's alliance with anti-choice (and anti-gay marriage) Senate Minority Leader Harry Reid. If Dean becomes head of the DNC, we should expect more of the same from him. <p>
But the lesson for abortion rights activists is not just to reject the Democratic Party as a vehicle for defending abortion rights. It's about coming to terms with the bankruptcy of the organizations that shill for the Democrats as well. <p>
I was not part of the fights against Operation Rescue in the early 1990s, but I think those lessons are incredibly important for us today and, I would welcome more background info on them in the pages of <i>SW</i>. We need to carry the politics and the history of the fight for abortion rights into our communities and into our unions to defend <i>Roe v. Wade</i>--and build a new movement that can roll back the attacks on choice that have been carried out in recent years.<br>
<b>Michele Bollinger,</b> Washington, D.C.<p>

