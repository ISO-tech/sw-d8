<font face="Times New Roman, Times, serif" size="4">Howard Dean selected as chair of Democratic National Committee</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Outsider" on the inside</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">February 18, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>ALAN MAASS looks at Howard Dean's selection as chair of the Democratic National Committee (DNC).</b><p>

HOWARD DEAN, the great hope of many liberals during the last presidential election, has become the Democratic Party's top bureaucrat.<p>
As chair of the DNC, Dean is in charge of keeping the party machine running. At the top of the job description: raise hundreds of millions of dollars from the party's richest donors.<p>
This doesn't exactly fit with Dean's image from the presidential primaries--as a crusading "outsider" who wanted to rip into George Bush and the Republicans. But that image was always a matter of style, not substance.<p>
Dean won grassroots support among rank-and-file Democrats because he was the only serious presidential candidate who was openly critical of Bush's war on Iraq--as well as the mainstream Democrats in Washington who rolled over for the Republicans during Bush's first term. Still, Dean's political views locate him squarely in the mainstream of the Democratic establishment--especially on domestic policy, where he claimed in one debate to be "much more conservative with money than George Bush."<p>
Dean won't be out of place in the Democrats' Washington headquarters because his views mirror those of the party leadership--something he made clear in his acceptance speech after the vote that made him DNC chair. "We cannot move forward if all we are is against the president and his administration," Dean told the cheering crowd of party officials.<p>
At a previous ceremony for the outgoing DNC Chair, Terry McAuliffe, Bill Clinton echoed the same themes. After defending his presidency by insisting that policies such as welfare "reform" left the party's base among workers and minorities "better off," Clinton said, "We will not...win again until we learn a few lessons."<p>
Clinton's wife, Sen. Hillary Clinton, has been offering those lessons to anyone who will listen--declaring last month, for example, that Democrats should seek to find "common ground" with anti-abortionists on a woman's right to choose.<p>
At a press conference following the DNC chair vote, Dean said Hillary Clinton's speech was "right on target."<p>
This is the prevailing wisdom among party officials: that the Democrats lost the 2004 election because they weren't conservative enough, especially on "values" issues. A <i>USA Today/</i>CNN/Gallup poll of DNC members found that only 16 percent thought Kerry himself was responsible for the Democrats losing.<p>
A separate poll of "political insiders" by the <i>National Journal </i>found that more than 80 percent of Democratic big wigs surveyed were satisfied that Dean would have a "positive" impact. So much for Dean being an "outsider."<p>
"Slap ol' Dean in a position that has little to do with policy, and much to do with corporate fundraising, they say--we'll at least get his grassroots cash," Joshua Frank wrote on CounterPunch, imagining the reasoning of party leaders. "Best of all, it'll shut the guy up. No more tangents. No more unscripted interviews. He'll have to tout the party line at every turn."<p>
Howard Dean was never the rebel his Deaniac supporters claimed he was. His ascension to the top of a rightward-moving Democratic Party establishment should be the final verdict on that illusion.<p>

