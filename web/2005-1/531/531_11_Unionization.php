<font face="Times New Roman, Times, serif" size="5"><b>Private-sector unionization is lowest in 100 years</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 18, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">UNION MEMBERSHIP in the private sector has fallen to its lowest level in a century as job losses hammer the heavily unionized manufacturing industries.<p>
According to the latest report by the U.S. Department of Labor, union membership in the private sector fell from 8.2 percent in 2003 to 7.9 percent in 2004--just half the level of 1983. Unionization in the public sector put the overall percentage of workers in unions at 12.5 percent--but this was a drop from 12.9 percent a year earlier.<p>
Organized labor does continue to have greater strength in particular regions. The percentage of workers in unions--known as union density--is 25.3 percent in New York and 16.5 percent in California. But the statistics highlight the enormous crisis gripping unions--not only in the historically anti-union South, but also in labor's base in the Midwest.<p>
Unions were hardest hit in Ohio, which lost 91,000 union members in 2003-2004, cutting the percentage of workers in unions from 16.7 percent to 15.2 percent. Illinois suffered a loss of 59,000 union jobs over the same period, with the percentage of workers in unions dropping from 17.9 percent to 16.8 percent.<p>
Manufacturing job loss also hit unions in the region where they can least afford a setback--the South--where much of the textile industry has shut down in the face of cheap imports or moved production overseas. North Carolina lost 14,000 union jobs, leaving just 97,000 union members in the state. With just 2.7 percent of workers in unions, the state is the least unionized in the U.S. Texas lost 41,000 union jobs and has a union density of only 5 percent.<p>
After the Civil War ended slavery, unions were repressed as a threat to the Jim Crow system of racial segregation. Ambitious efforts to organize the South were shelved in the early 1950s when union leaders purged labor's ranks of radicals during the anticommunist witch-hunts.<p>
As a result, unions have been largely excluded from the fast-growing Southern economy over the last half-century. Today, the 11 states of the old Confederacy have a union density of just 5.5 percent--less than half the already abysmal national average.<p>
The statistics also show why employers are determined to finish off unions, with median weekly earnings for private sector union members at $739, compared to $604 for their non-union counterparts. Yet here, too, are signs of decline. The median union pay-advantage for manufacturing workers fell from $63 per week in 2003 to $40 week in 2004, reflecting wage concessions in union contracts.<p>
Ironically, the statistics appeared shortly after the New Unity Partnership coalition of unions disbanded rather than press its case in the AFL-CIO on the need to organize the unorganized. As the hard numbers show, that debate is more urgent than ever.<p>

