<font face="Times New Roman, Times, serif" size="5"><b>Punk rock's elder statespeople</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 18, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2">The Mekons, <b><i>Heaven &amp; Hell: The Very Best of the Mekons, </i></b>released by Cooking Vinyl.</font><p>

<font face="Times New Roman, Times, serif" size="3">WHO'S LEFT from the punk rock revolution of the late 1970s? You can hear the influence of early punk bands like the Clash, the Ramones, X and the Buzzcocks in a lot of rock music today, but the punk pioneers themselves are seldom heard from--especially since the sad deaths recently of Joe Strummer of the Clash and Joey Ramone.<p>
Strangely, the mantle of punk rock elder statespeople has probably fallen on a band that was among the Least Likely to Succeed: The Mekons.<p>
The Mekons formed in the northern British town of Leeds, a group of unapologetic socialists who embraced punk's amateur ethic (why should not knowing how to play stop you from starting a band?) to such an extent that they could barely get through their first songs. Yet the Mekons are going strong after a quarter century of more or less continuous existence--a fact underlined by the recent release of their first "best of" compilation called <i>Heaven &amp; Hell.</i><p>
<i>Heaven &amp; Hell's </i>two discs have a good selection of the sprawling range of styles the Mekons have tinkered with over the course of more than 20 albums and at least a dozen other recordings. <p>
The rough edges of the band's punk era are represented by the early single "Never Been in a Riot," a barbed (and not entirely fair) criticism of the Clash. There's the influence of reggae on "Work All Week," which the band recently rerecorded in a "covers" album of its own early songs. "Work All Week" is also a great example of the Mekons' skill at writing songs that seem to be about personal and domestic situations, but turn out to be a sly critique of capitalism, too.<p>
Later Mekons albums tend more often toward country--not the prettified Nashville sound of today, but a rawer pre-commercial country--a direction that began when some core members began relocating across the Atlantic to the U.S. in the 1980s. But <i>Heaven &amp; Hell </i>documents other Mekon musical excursions through folk balladry, sampling and drum machines, New Orleans zydeco and--always, always--cranked-up guitar rock.<p>
This is surely the secret of long life. The Mekons have followed their musical interests across numerous pop-music pigeonholes, doing what they wanted, regardless of industry trends, and always with a sense of humor and of proportion.<p>
In the process, the band accumulated a rotating cast of members and collaborators who today hail from probably a half-dozen cities, and who drift in and out of various projects and performances, some Mekons-related and many others not. When assembled in some combination for a tour, the Mekons are as much fun to see live as they obviously have playing together--resembling a mischievous, unkempt and definitely Boss-less E Street Band crammed together on the tiny stage of whatever barroom they've been unjustly consigned to.<p>
Really, the Mekons are a kind of musical commune, bound together by each one's commitment to--and interest in--what the others are up to. <p>
This spirit was captured best on their last studio album, <i>OOOH! (Out of Our Heads), </i>released in 2002--with its glorious call-and-response, choral singing the centerpiece of what sounds like a gospel album for an alternative religion of resistance. For my money, it was the Mekons' best ever--and note that it came out when the band was 24 years old (which is, like, 107 in people years).<p>
<i>Heaven &amp; Hell </i>isn't as great, even though it collects a lot of great songs--they lose some of their distinctive personalities when taken out of their original album contexts. But for an introduction to the Mekons, it's a fine place to start.<p>

