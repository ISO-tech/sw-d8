<font face="Times New Roman, Times, serif" size="4"><b>Stop California's death penalty</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Kim Rabuck</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BERKELEY, Calif.--Seventy people came to hear Kevin Cooper, an innocent African American man, speak live from San Quentin's death row on the one-year anniversary of the date he was scheduled to be executed. California came within three hours of killing Cooper on February 10, 2004.<p>
Speaking via speakerphone, Cooper thanked everyone who had participated in the movement to stop his execution, and especially the Campaign to End the Death Penalty, without which, he said, he would not be alive today.<p>
Stopping Cooper's execution involved multiple press conferences, a signature ad in the <i>Sacramento Bee; </i>public meetings with Jesse Jackson, Cooper's attorneys and witnesses who offered new evidence of Cooper's innocence at Oakland's largest Black church; and a march of hundreds of angry protesters to San Quentin on the evening of the scheduled execution.<p>
Exonerated death row inmate Shujaa Graham flew out from Baltimore, Md., to commemorate the victory with Cooper. Graham has recently been fighting to keep New York from reestablishing the death penalty in that state.<p>
Barbara Becnel, editor and publisher of Stan "Tookie" Williams' recent memoir, <i>Blue Rage, Black Redemption, </i>and co-producer of the award-winning FX cable channel movie, <i>Redemption </i>(starring Jamie Foxx as Williams), also spoke at the event. She addressed the racism of the recent Ninth Circuit Court ruling, which failed to grant Williams a new trial despite clear evidence that prosecutors had struck all African Americans from the jury in his trial, which is a clear violation of his constitutional right to a trial by his peers.<p>
Former Democratic Gov. Gray Davis and current Republican Gov. Arnold Schwarzenegger have spent $990 million in the last 10 years to maintain California's death penalty. Today, as Schwarzenegger closes schools and denies health care to California's sick, he is spending $220 million more to add 400 more beds to San Quentin's death row.<p>
The audience--made up of students, former Black Panthers, members of Murder Victims' Families for Reconciliation and others--left the meeting with a renewed commitment to the struggle to stop California's racist death penalty.<p>
<i>For more information about how to join the fight to stop California's state-sponsored murder, go to www.nodeathpenalty.org.</i> <p>
