<p><a name="Occupation"></a><font face="Times New Roman, Times, serif" size="4"><b>End the occupation</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Karl Swinehart</b></font><p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES--Students at the University of California-Los Angeles (UCLA) held a "An Hour of Resistance" on February 8 to protest the occupation of Iraq. Photos of Iraqi checkpoints, victims of war and destroyed homes were displayed in the campus' main plaza.<p>
Some 150 students listened to speakers, including an Iraqi-American student, a speaker from ANSWER, Iraq Veterans Against the War cofounder Tim Goodrich and a speaker from the International Socialist Organization (ISO). They spoke out about the case of Pablo Paredes and encouraged students to sign and circulate his petition for conscientious objector status. The rally was followed by a march through the campus. <p>
At <b>Fremont High School </b>in South Central Los Angeles, nine students were suspended for refusing to participate in a military recruiting activity.<p>
Students were called in to take what was explained as an aptitude test, it was in fact the Armed Services Vocational Aptitude Battery, a military recruiting tool. When students saw the name of the test and the military personal proctoring the three-hour test, many began to protest. Students requested to leave, and the principal threatened anyone who left with suspension. Nine students left anyway.<p>
Commenting on the fact that most of the nine were Latina, the principal said that they should feel lucky, given that "there aren't many options in our community...Most Latinas end up working as housekeepers or at McDonalds."<p>
Students, parents and teachers are meeting and organizing to strategize how to confront this racist military recruitment.<p>
On February 17 at <b>East Los Angeles College, </b>Navy war resister Pablo Paredes will speak at a counter-recruitment forum sponsored by the ISO. Other speakers include mothers of soldiers in Iraq, members of Esperanza para Familias Militares and ELAC students working to get military recruiters off their campus. For more information, call 213-309-2713.<p>
