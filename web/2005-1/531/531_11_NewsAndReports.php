<p><a name="Police"></a><font face="Times New Roman, Times, serif" size="4"><b>Stop police violence</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Nathan Hensley</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AUSTIN, Texas--About 60 people met in front of the Austin Police Department (APD) headquarters on February 11 to protest the indiscriminate use of a Taser stun gun on activist Rene Perez during the January 20 counter-inaugural demonstrations. Perez was arrested after the attack, along with several other march participants. <p>
Concerned Citizens of Austin, the Austin People's Legal Collective and the Austin Spokescouncil called for the demonstration at the APD headquarters to show support for Perez, and to call attention to the darker side of the "non-lethal" Taser. Since 2001, at least 80 people have been killed by Tasers in North America alone. It's not hard to imagine; the Taser delivers a 50,000-volt shock to its unlucky victims. <p>
Who are those victims? In Austin, anyone guilty of verbal non-compliance with the police is eligible for a potentially deadly shock. This is hardly an alternative to the deadly force of firearms. Activists everywhere must be prepared to take on the police and their brutality, no matter how "non-lethal" it may seem.<p>

