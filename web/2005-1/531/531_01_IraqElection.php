<font face="Times New Roman, Times, serif" size="5"><b>The U.S. scheme for post-election Iraq</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 18, 2005 | Page 1</font><p>

<font face="Times New Roman, Times, serif" size="3">NOW THAT the results of the Iraqi election are finally in, Washington wants the winning parties to choose between its two favored candidates for prime minister.<p>
One is economist Adel Abd al-Mahdi, who used his role in the Iraqi transitional government to hand over the country's economy to U.S. oil companies. The other is Ahmad Chalabi, the discredited favorite of Pentagon hawks who helped George W. Bush sell his war on bogus pretenses.<p>
The reviving fortunes of Chalabi--whose office was raided by U.S. soldiers nine months ago after he was accused of tipping off Iran about U.S. spying efforts--underscore the real nature of Iraq's election at gunpoint. A vote promoted as an exercise in national self-determination and political expression for the long-oppressed Shiites is being used to repackage the U.S. occupation.<p>
The party bloc backed by leading Shia cleric Ayatollah Ali Sistani, the United Iraq Alliance (UIA), won the most votes in the January 30 election, as expected. Conveniently for Washington, the UIA fell just short of a majority, with 47.6 percent of the total.<p>
The results--finally released after a vaguely explained two-week delay--allow the U.S. to meddle in the horse-trading between rival Shiite parties that will control a majority of seats in the new National Assembly.<p>
This opened the door for Chalabi to present himself as a candidate for prime minister or another top government post. He has the backing of the more militant Shiite cleric Moktada al-Sadr, whose followers have fought major battles with U.S. occupation forces. Still, the Sadr-Chalabi connection didn't deter a top official of the U.S. Embassy from making a high-profile post-election visit to Chalabi to discuss their mutual interests.<p>
The prime minister will be chosen by a three-person presidential council, designed by the U.S. to include a Shiite, a Sunni Muslim and a Kurd. <p>
The U.S. is trying to play off the Shiites against the Sunnis, as well as the Kurdish minority, which wants autonomy--and control of the city of Kirkuk, along with its nearby oil. The Sunnis, formerly the dominant political group in Iraq, overwhelmingly boycotted the elections and continue to be the main base of the armed resistance.<p>
While it's impossible to predict the outcome of the wheeling and dealing, Washington has already signaled that its choice for prime minister is Mahdi, whose abilities as a political chameleon will be highly useful. An economist and former member of Saddam Hussein's Baath Party, Mahdi became a Maoist in France in the 1960s. He later moved to Iran and signed up with the Shiite Supreme Council for the Islamic Revolution in Iraq (SCIRI), which fought with Iranian troops against Iraq in the Iran-Iraq War of the 1980s.<p>
For the U.S., Madhi's chief qualification is willingness to carry out a free-market, neoliberal economic agenda. "Al-Mahdi is the Bush administration's Trojan horse in the UIA," wrote left-wing columnist Naomi Klein, noting Mahdi's role in signing deals with Shell, BP and ChevronTexaco, negotiating an austerity plan with the International Monetary Fund, and promising privatization and a concessionary oil law to U.S. investors. <p>
The other leading Shiite candidate for prime minister, Ibrahim al-Jaafari of the Dawa party, also of the UIA, also opposes a U.S. withdrawal. "If the United States pulls out too fast, there would be chaos," he told a reporter.<p>
All this flies in the face of the UIA's nationalist election campaign slogans calling for an end to the occupation. "Sistani sold the elections to the pious Shiite masses as the first step toward the end of the occupation," wrote <i>Asia Times </i>journalist Pepe Escobar. "In the next few months his promise will be subjected to a groundbreaking reality test. Shiites at the polls unmistakably said that they were voting to expel the Americans, not to legitimize them."<p>
The new government's main task is to write a new constitution--and then hold new elections at the end of the year. The U.S. will use the intervening time to postpone a "status of forces" agreement on U.S. bases in Iraq--and to try to consolidate its grip on Iraq.<p>
Despite the election-driven talk of establishing "conditions" for a withdrawal of U.S. troops, Washington is determined to remain in Iraq--and won't leave until it's forced to do so.<p>

