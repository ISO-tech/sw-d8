<font face="Times New Roman, Times, serif" size="5"><b>Activists challenge Horowitz's lies</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Dana Cloud</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 22, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">RIGHT-WING ideologue David Horowitz had protesters arrested last week at the University of Texas Law School--where he was speaking on the high value he places on free expression.<p>
About two dozen demonstrators came out to challenge Horowitz, carrying signs that read "Dissent is not terrorism," "Horowitz=McCarthy" and "No new witch-hunts." Horowitz--who was sponsored on campus by the right-wing Intercollegiate Studies Institute and the Federalist Society for Law and Public Policy Studies--refused to begin speaking until protesters put down their signs. <p>
His message: Shut up while I talk about free speech. When we refused, police at the event upheld our right to hold signs. Some activists had taken up positions around the room and began shouting at Horowitz when he was speaking. The hosts of the meeting told us we would be arrested for verbal outbursts, so protesters used their cell phones and small air horns to disrupt the speech.<p>
One young woman exclaimed in protest when Horowitz explained that there is no racism in the U.S.--stating as proof the fact that Oprah Winfrey ("a Black woman with a weight problem") is the richest woman in America. She was escorted from the room and arrested. Another woman was arrested on "suspicion of having an air horn."<p>
In total, six protesters were arrested and charged with disruption of the meeting. The offense could carry a $2,000 fine or 180 days in jail.<p>
Horowitz claims that he stands for free expression, but his goal is to encourage students to monitor and report progressive faculty for antiwar activities, criticism of U.S. policy and challenges to conservative ideas and values. His speeches are filled with falsified anecdotes of left-wing misconduct, and he maintains an Internet blacklist of left activists and academics--whom he equates with terrorists and names as enemies of the state. <p>
The University of Texas campus has erupted in controversy since Horowitz's appearance, as the right-wing charges protesters with violating his free speech rights. In fact, the arrests show who is really willing to repress free expression. Urgently needed efforts are underway to build alliances among left faculty and student organizations to defend the protesters--and to fight McCarthyism on the University of Texas campus.<p>


