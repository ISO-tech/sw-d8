<font face="Times New Roman, Times, serif" size="4">NBC's take on British sitcom <i>The Office</i></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Meet the new boss</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 22, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>The Office</i>, </b>airs Tuesdays at 9:30 Eastern time on NBC.</font><p>

<font face="Times New Roman, Times, serif" size="3">CONVENTIONAL WISDOM says that white-collar office workers have it better than their blue-collar brethren. But anyone who's ever done time as a cubicle rat in the kind of soul-sucking 9-to-5 existence chronicled in NBC's new sitcom, <i>The Office,</i> knows that bosses pretty much suck, no matter what color collar you wear.<p>
Based on the droll British smash of the same name, <i>The Office</i> is set in Dunder Mifflin, a paper supply company in Scranton, Pa. The premise--a documentary film company is on hand to chronicle the inner workings of the company's daily grind--allows us to see that, yes, behind the scenes, boss Michael Scott (played to perfection with a deadpan, grating idiocy by <i>Daily Show</i> alum Steve Carell) is actually just as boorishly clueless in private as he is in front of his sour, downtrodden employees.<p>
In the tradition of self-important bosses everywhere, however, he labors under the delusion that he is not only a master comedian and a morale booster, but universally loved by his underlings. "I think that pretty much sums it up," he knowingly tells the film crew, pointing to his "World's Best Boss" coffee mug, before adding, "I found it at Spencer Gifts."<p>
Meanwhile, with the exception of Dwight--Michael's bootlicking toady with the flair for petty despotism of small-minded bureaucrats--the employees at Dunder Mufflin universally despise him. Think it's better to have a boss who thinks he's your friend--while ignorantly screwing with your life on a daily basis--than a boss that openly hates you? Guess again, says <i>The Office</i>.<p>
Refreshingly free of P.C.-bashing, the funniest episode so far has been about a company "Diversity Day." After Michael offends the staff by doing his version of a Chris Rock routine, corporate holds a mandatory racial-sensitivity training. <p>
Eager to prove to the higher-ups that he can be "innovative" (and totally oblivious to the fact that his behavior was offensive), he responds by hosting his own rival "diversity" session. In one of the show's funniest lines, Michael explains his antiracist vision by saying, "As Abe Lincoln once said: If you are a racist, we will attack you from the north."<p>
He later asks Oscar, the office's lone Hispanic worker, "Is there a term besides Mexican that you prefer? Something less offensive?"<p>
Another episode has Michael forcing underlings to plan a surprise birthday party--for a woman who's birthday is a month away--to boost "morale" after rumors of downsizing. This forced frivolity (which a former coworker of mine used to refer to as "sending the clowns into the cancer ward") is made even more grotesque as Michael delivers jaw-dropping lines to the "birthday girl" like: "Meredith--let's hope the only downsizing that happens to you is that someone downsizes your age...because of the downsizing...rumors...and because you're getting old."<p>
Thankfully, with the exception of a fledging crush between receptionist Pam and shy, good guy salesman Jim, the show doesn't take the easy way out by making the office underlings full of warm fuzzies for each other.<p>
Refreshingly free of laugh tracks and musical cues, <i>The Office </i>is best when it's at its snarkiest and most viciously, blackly humorous. Viewers familiar with the British version might complain that the U.S. show isn't as dryly funny, and that Carell isn't quite as good at being offensive as his British counterpart Ricky Gervais. That may be true to a small extent--but <i>The Office</i> is still one of he funniest shows on U.S. network television.<p>
My advice to white-collar workers looking to hold onto their sanity? Steal company supplies. Surf the Web as much as you can. Make long distance calls on the company dime. Churn out as many free photocopies as you can smuggle out. And watch <i>The Office.</i><br>

