<font face="Times New Roman, Times, serif" size="4">Blue-collar workers coordinate action at nine Calif. campuses</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>UC hit by one-day strike</b></font> <p>
<font face="Arial, Helvetica, sans-serif" size="2">April 22, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">THOUSANDS OF blue-collar service workers represented by the American Federation of State, County and Municipal Employees (AFSCME) walked out April 14 for a one-day unfair labor practices strike on all nine University of California (UC) campuses.<p>
Clerical workers in the Coalition of University Employees (CUE) and the professional and technical workers in the Union of Professional and Technical Employees (UPTE)--both stalled in contract negotiations--also joined the picket lines. Graduate student employees, represented by the United Auto Workers, canceled many classes and walked the picket lines, too.<p>
AFSCME--which represents food servers, janitors and other low-paid workers--has been fighting for a new contract since June 2004. Although wages have been frozen for two years, UC won't guarantee a pay increase in the new contract--even though last year it found $2.4 million to hand out in bonuses for just 65 people at the top of the university system's administration. UC also refuses to consider bargaining demands about job safety and respect, including seniority in hiring, promotions and pay, fair and safe workloads for custodians, and sweatshop-free uniforms for all workers.<p>
At the strike authorization vote in March, workers turned out in record numbers and dramatically showed their anger with a 92 percent "yes" vote--clearing the way for AFSCME's first strike in 29 years.<p>
At UC-Berkeley, the action began before sunrise when picketing AFSCME workers turned back more than 100 unionized construction workers at four sites. Dozens of professors were convinced to move classes off campus or cancel them entirely as students came out to the picket lines by the hundreds. At a noon rally, more than 1,000 students and workers listened to solidarity greetings from workers from other unions.<p>
Meanwhile, at UC-Santa Cruz, mass pickets of over 1,000 workers and students lined the two major entrances to campus. More than 500 students took part in a mass civil disobedience, blocking the main entrance to campus for more than seven hours and forcing police to divert traffic.<p>
"We are united, and we're all working together today," UC-Santa Cruz worker Linda Biancalana, who cleans dormitories, told <i>Socialist Worker</i>. "After working here for 18 years, I would be making about $13 an hour--the same as I was making 30 years ago. Now, I'm going backwards, and it's 2005."<p>
Another 750 AFSCME members were on strike at the UC-Davis campus and medical center, where members of other unions and students also joined picket lines to shut down several major construction sites. Teamster drivers from UPS and other companies also refused to cross picket lines.<p>
At UC-San Diego, about 950 AFSCME members received similar solidarity from other campus unions and students. "It's offensive that this university pays any employee poverty wages," UPTE Local 9119 bargaining team member Art Daily said on the picket line.<p>
At UCLA, campus workers and their supporters demonstrated in front of the medical center and main entrances to the university, converging on Bruin Plaza with a mass picket of 1,000 people. "It doesn't make sense that UC workers qualify for nine different welfare programs," AFSCME Local 3299 President Lakesha Harrison told the rally. "The top administrators in the UC system gave themselves $2.4 million raises." <p>
Many strikers described the routine racist discrimination they face. "They say they won't give us a raise because our English isn't good enough," said Maribel Herrera, who has worked at UCLA for eight years. "They say we're not trained enough to get a raise, but they don't give us any training! They fired the one supervisor that ever offered us any training."<p>
AFSCME has threatened to launch an all-out strike if UC doesn't dramatically improve its offer. While the support from other unions was impressive, it could have been stronger on some campuses. Solidarity among all the workers on the campuses will be crucial in winning this struggle--and April 14 showed the potential to gain an important labor victory.<p>
<i>Jocelyn Blake, Noah Brick, Vernon Jordan, Andy Holguin, Martin Smith and Karl Swinehart contributed to this report</i>.

