<font face="Times New Roman, Times, serif" size="5"><b>Oregon high court nullifies gay marriages</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Chris Mobley</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 22, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">IN A blow against equal marriage rights, the Oregon Supreme Court voided more than 3,000 marriage licenses issued to gay and lesbian couples last spring. The nullification came in the court's ruling on a lawsuit filed by the American Civil Liberties Union (ACLU) on behalf of eight same-sex couples and the gay-rights group Basic Rights Oregon (BRO) that sought full rights for same-sex couples. <p>
Multnomah County, home to Portland, began issuing marriage licenses to same-sex couples in March of last year, after a county attorney said denying them to gays and lesbians would violate Oregon's constitution. Six weeks later, a judge ordered the county to stop issuing the licenses, and Democratic Gov. Ted Kulongoski later ordered the state registrar not to recognize the marriages. <p>
A circuit court judge originally found in favor of the ACLU's lawsuit protesting Kulongoski's move. But during the appeals process, opponents of gay marriage--under the umbrella of the Defense of Marriage Coalition--began gathering signatures for Measure 36, to amend the state constitution to ban gay marriage. This measure passed by a slim margin during the November election. <p>
Coming out of this defeat, many gay-rights groups are now advocating for legislation legalizing civil unions, which Kulongoski recently announced he would support. "This is not an abstract issue," said Jamie Pedersen, a Seattle lawyer with Lambda Legal, a national gay and lesbian civil rights group. "This is an issue that involves very real harm that comes to couples because their relationships don't have any protection."<p>
Gay and lesbian couples need legal protections, but civil unions don't offer enough of the rights these couples deserve. The strategy of pushing for civil unions undermines our ability to argue for why same-sex couples deserve full marriage rights. <p>
We shouldn't accept back-of-the-bus treatment for gays and lesbians. We want equal marriage now!<p>

