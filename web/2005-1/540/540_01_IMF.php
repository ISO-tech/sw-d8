<font face="Times New Roman, Times, serif" size="5"><b>IMF turns the screws on poor countries</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 22, 2005 | Page 1</font><p>

<font face="Times New Roman, Times, serif" size="3">THE RICH countries are promising debt relief for poor nations sometime in the future--even as they keep demanding more austerity and free-market reforms in countries like Ecuador. That was the public relations spin at the annual spring meeting of the International Monetary Fund (IMF) and World Bank April 15 and 16, where finance ministers from the Group of Seven industrialized nations once again promised--but failed to deliver--a plan to forgive the debts of the world's poorest countries.<p>
Meanwhile, in Ecuador's capital city of Quito, mass protests against IMF-mandated austerity plans and the government's authoritarian measures threatened to topple President Lucio Guiti&eacute;rrez. Ironically, Guiti&eacute;rrez was elected in 2002 on promises to oppose free-market policies, which have left more than 62 percent under the poverty line in the Andean nation of 13 million people.<p>
Outgoing World Bank President James Wolfensohn used the IMF and World Bank meetings to criticize the rich countries for spending little on alleviating debt and fighting poverty in developing countries. But it was on Wolfensohn's 10-year watch that the World Bank became even more of a tool of Western governments and banks--as it pushed the free-market agenda, tying aid and loans to pro-market, neoliberal measures known as "structural adjustment."<p>
That term has gone out of fashion as social movements across Latin America have rejected neoliberalism and voted--or forced--out conservative governments in Venezuela, Bolivia, Brazil, Argentina and Uruguay. The global justice movement, although weak in the U.S., has kept up the pressure on international financial and trade institutions as well.<p>
But the neoliberal offensive continues, repackaged as support for "human development." Just last month, the World Bank approved a new $100 million loan for Ecuador--where the total debt is already equivalent to 61 percent of Gross Domestic Product. Some 40 percent of Ecuador's government spending goes to repay external debts. <p>
In exchange for the loan, the Guiti&eacute;rrez government agreed, in the World Bank's words, to "reverse expansionary spending"--that is, cut the budget, and "enhance labor flexibility" by slashing jobs and weakening workers' rights.<p>
The World Bank loan followed a February visit to Ecuador by IMF Managing Director Rodrigo Rato, who served as finance minister in Spain's former right-wing government. Sounding like the Spanish colonial viceroys of old as he serves today's U.S. empire, Rato declared that Ecuador "must open its oil, electricity and pension sectors to private investment," as an official IMF briefing put it. Further, Ecuador needs "changes to labor market rules, restructuring of public enterprises and changes to social spending to provide more help to the poor majority."<p>
Toeing the IMF line, Guiti&eacute;rrez recently unveiled those "changes"--a proposed law of "economic rationalization" that would privatize the pension system, dramatically raise the costs of electricity for consumers, sell off state enterprises to private capitalists, cut taxes to businesses and reduce workers' benefit funds.<p>
At the same time, U.S. is pressing Ecuador to sign a free-trade deal that would help build up pressure for Washington's proposed Free Trade Area of the Americas. The U.S. has already enlisted Guiti&eacute;rrez's in Plan Colombia, which militarizes the Andes in the name of the "war on drugs."<p>
While the resistance in Iraq may have slowed the U.S. offensive in Latin America, Washington now aims to use the IMF and World Bank--and former populists like Guiti&eacute;rrez--to regain the initiative. And with the Pentagon's Paul Wolfowitz set to take over, the World Bank will become even more closely integrated with Washington's policy. The main architect of the U.S. invasion of Iraq will now administer one of the economic arms of imperialism--and toward the same goal he pursued by military means: Washington's world dominance. <p>
The struggle in Ecuador, however, has once again shown the breadth of popular opposition to Washington's program--and highlights the need to rebuild the movement for global justice. <p>


