<font face="Times New Roman, Times, serif" size="5"><b>Why are two 16-year-old Muslim girls in a federal jail?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lucy Herschel</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 22, 2005 | Pages 1 and 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE U.S. government stooped to a new low in its witch-hunt of Muslims when federal agents detained two 16-year old Muslim high school students from New York City. <p>
Relying on secret "evidence," the FBI claims that the young women present "an imminent threat to the security of the United States based on evidence that they plan to be suicide bombers." Advocates for the girls' families believe that charges stem from a misinterpretation of an essay written by one of them, in which she actually argues that suicide bombing is against Islamic law. <p>
The FBI also claims that the two teenagers attended the same fundamentalist mosque--though their parents say they first met after they were detained at the end of March. <p>
The two young women, one born in Bangledesh and the other in Guinea, are both undocumented immigrants who came to the U.S. as small children and have lived here almost all their lives. <p>
They are being held in Leesport, Penn., a four-hour drive from their homes. Their phone calls are restricted to one a week. Only their parents are allowed to visit them, for 15 to 20 minutes at a time. According to the immigrants' rights group Desis Rising Up and Moving (DRUM), at least one of the girls was illegally interrogated without legal representation and has been threatened with the deportation of her parents if she doesn't admit to the allegations. <p>
Teachers and classmates of the two 16-year-olds have expressed outrage at their detentions. As a friend's mother--and head of the parents' association at the East Harlem high school she attends--told the <i>New York Times, </i>"I feel like, how dare they? She's a minor, and even if she's not a citizen, she has rights as a human being." <p>
This case comes in the midst of a push to expand the federal government's Homeland Security powers. "My sense is that with the new Bush administration installed, the national security apparatus is primed and pumped and aching to use its new powers," said Adem Carroll, of the Islamic Circle of North America, who has been working with the girls' families. "Congress is currently considering minor modifications and perhaps expansion of the Patriot Act; the Real ID Act is being debated; other surveillance systems are being prepared."<p>
DRUM Legal and Policy Organizer Kavita Pawria agreed. "I think it's part of the government's message to let citizens know that it hasn't let down its guard around "terrorism" or "national security"--and to continue keeping immigrants in fear of the government, which is an ideal way to silence organizing and resistance by our communities," Pawria told <i>Socialist Worker</i>. <p>
But the Bush administration may have overplayed its hand this time. The detention of the two girls has generated anger at their schools and throughout the city. Classmates are circulating a petition to support them. Local immigrants rights activists have begun to organize around these cases, and had planned a rally outside one of the girls' bond hearing last week--until the hearing was postponed again.<p>
