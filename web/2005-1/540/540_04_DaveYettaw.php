<font face="Arial, Helvetica, sans-serif" size="2"><b>OBITUARY: DAVE YETTAW</b></font><br>
<font face="Times New Roman, Times, serif" size="4">A man who personified the UAW's best traditions</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>He was a lifelong militant</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 22, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">THE LABOR movement suffered an irreplaceable loss April 14 when Dave Yettaw passed away at the untimely age of 58.<p>
As a longtime rank-and-file activist and later local president in the United Auto Workers (UAW) at General Motors' Buick City complex in Flint, Mich., Dave was a key leader of the New Directions Movement that challenged the contract givebacks and pro-corporate policies of the UAW in the 1980s. As president of UAW Local 599, he put his perspectives to the test, leading an important strike in 1994 that showed the union's power by winning hundreds of new jobs after years of devastating cuts.<p>
That victory rattled both GM execs and top UAW leaders, who conspired to oust Dave and the New Directions slate in the next local union election. "Vote for Yettaw and New Directions, and GM will close the plant," Yettaw's opponents said. Dave and his team lost--and GM closed the plant anyway.<p>
As a retiree, Dave ran with a New Directions slate as delegates to the 1998 UAW convention and won, elected by workers who felt betrayed by the UAW. The convention took place amid a long strike at two Flint parts plants that had virtually shut down GM's entire North American production system.<p>
I'll never forget how the late Steve Yokich, then UAW president, sweated and stammered whenever Dave took to the convention floor to call for a more militant approach to the struggle. Yokich, with his big salary and gold-plated benefits, was far more comfortable golfing with Ford executives than leading strikes. Dave, by contrast, was the real thing: a lifelong militant who personified the best traditions of the UAW. And Yokich knew it.<p>
Even in his retirement years, Dave kept fighting to challenge the direction of the UAW. He was always willing to put his encyclopedic knowledge of the UAW's contracts, constitution and appeals process at the disposal of activists across the country. He helped people overturn stolen elections, win back their jobs, strategize how to vote down lousy contracts, and bring issues to the UAW convention floor. <p>
As an authority on the real history of the UAW--including the central role of radicals, socialists and communists in the union's early years--Dave was a one-man school of what the old-timers called class-struggle unionism. "The [UAW leadership] is taking this union back to where we were in 1933, when we had company unions," he told me in an interview for <i>Socialist Worker </i>about the 20003 contract.<p>
I got to know Dave through UAW conventions and reform meetings in the last seven years. He was an invaluable resource to those of us in the reform wing of the National Writers Union--a local of the UAW--as we ousted incumbents backed by the union hierarchy.  <p>
Like scores of other UAW members, I corresponded regularly with Dave. We also spent many long hours on the phone, discussing not only the UAW and the labor movement, but the rightward turn in U.S. politics. Dave once told me that he was getting more radical as he got older. A Vietnam veteran, he spoke out against the impending war in Iraq at a grassroots meeting of UAW retirees in late 2002.<p>
Sadly, we've lost Dave just as he was warming to a new fight against cuts in the Big Three health care plans and retirement benefits. Just three days before he passed away, he was in his element, meeting with other activists to strategize.<p>
My favorite memory of Dave is from the 1998 union convention. By patiently asking a series of pointed questions, Dave had prodded Yokich into making a militant speech about the GM strike. "When he's on that road," Dave said afterward, with a twinkle in his eye, "you push him down it as far as you can."<p>
With Dave gone, it's up to us to keep pushing. The job is will be much harder without him. But we can keep learning from his example.<br>
<b>Lee Sustar, </b>Chicago<p>

