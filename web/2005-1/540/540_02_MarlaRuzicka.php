<font face="Arial, Helvetica, sans-serif" size="2"><b>OBITUARY: MARLA RUZICKA</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>She struggled for a better world</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Todd Chretien</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 22, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">U.S. ACTIVIST Marla Ruzicka was killed in Iraq on Sunday at the age of 28. According to press reports, she was killed, along with her Iraqi colleague, when a suicide bomber attacked a U.S. security convoy driving ahead of them.<p>
I first met Marla after the Battle of Seattle in the early days of the global justice movement, when she was working for Global Exchange in San Francisco. She was in her early 20s then, and expressed the confidence and excitement of a whole generation of people who believed that a better world was possible. <p>
When Ralph Nader ran for president in 2000, Medea Benjamin--Marla's mentor and hero--ran as the Green Party candidate challenging California's Democratic Sen. Dianne Feinstein. Marla and I worked side by side on Medea's campaign, and I got to know her as a warm and caring person. <p>
In the wake of the September 11 attacks, a string of hate crimes against Muslims and Arabs broke out, and we drove five hours from San Francisco to Fresno together to attend the funeral of a man killed because of his race--to bring solidarity to the family and make connections with communities under attack. Marla couldn't stand the injustice of it. She seemed to visibly shrink in sorrow for the family. <p>
Her compassion led her to Afghanistan and Iraq to do what she thought was right--by demanding compensation from the U.S. government for the civilian victims of the twin invasions and occupations. <p>
Tragically, like the Afghan and Iraqi people for whom she advocated, Marla became a casualty of this war for oil and empire. She would be the last to want her death to be used as an excuse to continue U.S. conquests. <p>
Instead, she would want her own name added--without distinction of nationality or religion--to the list of more than 100,000 Iraqis killed since the U.S. invasion in March 2003, and the millions more killed by sanctions before the war. <p>
And she would want us to fight like hell for a world based on justice and peace that she died believing in.<p>


