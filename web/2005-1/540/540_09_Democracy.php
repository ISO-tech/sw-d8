<font face="Times New Roman, Times, serif" size="5"><b>What the U.S. means by "democracy"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 22, 2005 | Page 9</font> <p>

<font face="Times New Roman, Times, serif" size="3">PRESIDENT BUSH and his neoconservative acolytes often chide their critics as being opponents of "democracy" in the Middle East or believing that Arabs and Muslims aren't "ready" for democracy.<p>
For those of us on the left, this charge is baseless. In fact, the left has been in the forefront of fights for genuine democracy and the rights of the oppressed in the region. But what Bush really means is that there is only one kind of acceptable "democracy" in the region--the pro-U.S./pro-Israel kind.<p>
"Since one cannot promote freedom and install a puppet government at the same time, Mr. Bush was adamant about holding elections. The crux of the matter is that the goal of these elections is not to transfer power and authority to the Iraqi people, but rather to legitimize ongoing U.S. control in the region," wrote Israeli human rights activist Neve Gordon recently.<p>
Those in and outside the region who are skeptical of U.S. motives in promoting democracy today after backing dictators for years are right to be skeptical. Even the U.S. government's own Defense Science Board challenged Bush's simpleminded mantra of "democracy."<p>
"Today we reflexively compare Muslim 'masses' to those oppressed under Soviet rule. This is a strategic mistake...Muslims do not 'hate our freedom' but rather, they hate our policies."<p>
The U.S. suffers from "a fundamental problem of credibility." People in the Middle East can see that the "American occupation of Afghanistan and Iraq has not led to democracy there, but only more chaos and suffering."<p>
The Israeli- and U.S.-promoted advocates of "democracy" in Lebanon and Iran--like their Iraqi counterpart, Ahmad Chalabi and his Iraqi National Congress--are traditional allies of Israel and the U.S. in the region. They are a strange flock of the Lebanese far-right and monarchist elements in Iran that fly under the names of the United States Committee for a Free Lebanon, the Foundation for the Defense of Democracies, the Lebanese American Council for Democracy, and the Coalition for Democracy in Iran.<p>
These groups and their affiliates, with strong patrons among the neoconservative think-tank establishment, are also chief recipients of U.S. aid under the National Endowment for Democracy and other innocuous-sounding aid agencies. All of these groups are the main supporters of the Syrian Accountability and Lebanese Sovereignty Restoration Act of 2003 that sanctions Syria and Lebanon until Syria withdraws its forces from Lebanon and until both countries recognize Israel.<p>
Needless to say, they have very little to do with democracy in Lebanon or anywhere else.<p>
But democracy should mean more than simply regular elections that ratify a U.S.-approved status quo. It should mean social rights for the oppressed and exploited and self-determination of nations. It should mean the ability of people of the region to control their own resources.<p>
This is precisely the type of the democracy in which the U.S. has no interest. If the Iraqi people were given the right to vote in a referendum on whether the U.S. should get out their country and keep its hands off of their oil, such a referendum would pass overwhelmingly.<p>
Therefore, under the current U.S.-controlled system, there will never be such a referendum. Likewise, despite their constant criticism of Yasser Arafat for being a corrupt autocrat, the U.S. and Israel preferred Arafat's (and now Abu Mazen's) corruption and dictatorial ways to the grassroots democracy from below built in the struggle of the first Intifada (1987-1991).<p>
The struggle for real democracy and self-determination will develop hand-and-hand with the struggle against imperialism--and its collaborators in the oppressed countries.<p>
On April 9, 2005, the second anniversary of the U.S.-staged toppling of Saddam's statue in Baghdad's Firdos Square, hundreds of thousands of Iraqis rallied to demand that the U.S. get out of their country. The demonstrators toppled effigies of Bush and the other leading imperialist advocate of Middle East "democracy," British Prime Minister Tony Blair.<p>
As this movement gains steam, Iraq could become another kind of example of democracy--one exactly opposite of what Bush hopes.<p>

