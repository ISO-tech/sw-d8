<font face="Times New Roman, Times, serif" size="5"><b>Vote no on UTLA contract</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By David Rapkin, United Teachers Los Angeles</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 8, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES--Soon after Los Angeles teachers voted out most of their old union leadership, the ousted officials concluded a tentative contract agreement March 22 with the school district after having allowed negotiations to drag on for more than 20 months. The proposed deal is an insult to all United Teachers of Los Angeles (UTLA) members and ought to be rejected when it goes to a ratification vote later this month.<p>
The incoming leaders, members of the reform United Action slate, won't take office until June 30. But they've already launched a public campaign to call on UTLA members to vote "no" on the offer.<p>
After five years during which LA teachers have seen only a 3 percent increase in salaries, the new offer includes 0 percent for the first year (2003-04), 2 percent for the second year, and a re-opener to negotiate salary for the third year of the pact. <p>
Historically, re-openers have meant zero pay increases for UTLA members. "Teachers at least deserve to keep their heads above water, and this contract has us drowning," said Julie Washington, the newly elected elementary vice president. "Two percent over three years doesn't even cover the increase in the cost of gasoline."<p>
While the current UTLA Board of Directors voted narrowly by a margin of 22-20 to recommend the contract to members, the broader and more militant union House of Representatives voted nearly 140-80 to recommend a "no" vote.<p>
The lousy raise--effectively a pay cut, once inflation is taken into account--is only one reason to reject the contract. Unfortunately, the tentative agreement makes no mention of class size, while overcrowded classes continue to ruin educational opportunities for students throughout LA. <p>
In addition, the contract institutionalizes a series of practices that attempt to get teachers to "team" as junior "partners" with administrators on committees that our bosses control. The tentative agreement also does nothing to address the needs of substitutes, bilingual teachers, special education teachers and adult education specialists.<p>
Shamefully, the outgoing union leadership is trying to sell the agreement dishonestly. By adding the years 2002-03--actually part of the previous contract--and by claiming that increases in health care costs are part of a "salary increase," outgoing UTLA President John Perez is claiming that the "total compensation increase" in the agreement is 10.1 percent.<p>
The "no" vote campaign will be the first test of the new United Action leadership, elected as a fighting alternative to the Perez leadership. "The membership has deserved more for a long time," said United Action's Washington. "But union leaders can't do it alone. It's time, now, for the members of UTLA to mobilize and organize if they indeed want something better."<p>

