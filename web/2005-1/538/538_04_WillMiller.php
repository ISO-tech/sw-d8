<font face="Arial, Helvetica, sans-serif" size="2"><b>OBITUARY: WILL MILLER</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>One of the heroes of our movement</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 8, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">WILL MILLER has died. The cancer that he had struggled with over the last year finally took him from us. Everyone who had a chance to take his classes, work with him and struggle alongside him knows that we have lost one of the heroes of our movement. <p>
Will combined tremendous, almost encyclopedic, memory of history with a tireless commitment to struggling for a better world. As our area's most beloved Marxist, he embodied the old Moor's famous dictum, "Philosophers have only interpreted the world in various ways; the point, however, is to change it." <p>
Will's passing makes this bright, warm, spring day a mournful one. Will's death is a tremendous loss for all his friends, family and his comrades in the struggle for a better society.<p>
We should take time today to remember him. I will never forget his talks about U.S. imperialism--how he would begin with its genocide against Native Americans and trace its development all the way through its many crimes committed over the last 100 years. He would recount the history will scientific accuracy, adjusting his glasses, repeating his favorite phrases, like "I take it that." <p>
But throughout, he never gave into despair, as he would always point to the resistance and the fight for liberation that was the antidote and hope amidst the horror.<p>
My favorite story that he just told in an interview was of a demonstration against the Vietnam War in Washington, D.C. It was the famous march to shut down D.C., when tens of thousands of activists engaged in civil disobedience throughout the city. <p>
Will joined hundreds of Vermonters to blockade one of the bridges from Alexandria into D.C. The Army ordered a group of mainly Black paratroopers that had just returned from Vietnam to affix bayonets to their weapons and attack the activists. The Black paratroopers refused the order and joined the demonstration. In that moment, Will said, he got a glimpse of what a socialist revolution would look like in the U.S.<p>
Will has died, but like his hero John Brown, his soul goes marching on--it goes marching on in each one us who will continue the struggle for socialism. He has passed the red banner onto us, and we take it up today in his honor. <p>
In the words of Latin American activists, who remember their fallen comrades by declaring them still here with us, still fighting alongside us: "Will Miller, Presente!"<br>
<b>Ashley Smith, </b>Burlington, Vt.<p>

