<font face="Times New Roman, Times, serif" size="5"><b>The foul anatomy of a fascist rally</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Dennis Kosuth</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 8, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>The Letter: An American Town and the "Somali Invasion," </b></i>written and directed by Ziad Hamzeh. Visit <a href="http://www.hamzehmystiquefilms.com" target="_blank">www.hamzehmystiquefilms.com</a> on the Web for screening information.</font><p>

<font face="Times New Roman, Times, serif" size="3">DURING THE 2004 presidential election, many progressives looking for ways to line up votes for the Democrat, John Kerry, argued that the Bush administration would usher in fascism. This misunderstanding of what fascism is and how such a movement could arise in this country might be served by viewing the disturbing documentary <i>The Letter</i>. <p>
The film tells the story of some of the 1,100 refugees fleeing violence of civil war in Somalia. Placed in Atlanta by the federal government, some families find the seemingly quiet but cold streets of Lewiston, Maine, more conducive to raising their families--and thus begin a second migration northbound. <p>
Hundreds of Muslims moving to a predominately white community prompts Lewiston Mayor Larry Raymond to write a letter to a local newspaper, bluntly asking Somalis to stop moving into town. This only feeds the distrust of some locals, already suffering from unemployment and poverty that has been left in the wake of a fleeing steel industry. Bob Dylan's song, "Only a Pawn in Their Game," about poor whites and racism in the U.S. South, couldn't be more apt to the footage of the out-of-work white Mainers, as they spout lies about immigrants getting food stamps and free cars.<p>
A motley bunch of neo-Nazi groups sees in Raymond's letter an open invitation to swoop in and feed on the hate caused by joblessness and racism. On January 11, 2003, with Raymond out of town on "vacation," and the racists hold their rally. Fortunately, there is opposition in the town. Two counter-demonstrations are held--one, a 4,000-strong rally across town, and a second smaller one organized to directly confront the racists.<p>
The film is short on political analysis, leaving viewers to draw their own conclusions about how to oppose racism. But it is still a stark view into the anatomy of a fascist rally, in all its disgusting detail.<p>


