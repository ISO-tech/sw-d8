<font face="Times New Roman, Times, serif" size="4"><b>Stop racism at McDonald's</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Noreen McNulty</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--On March 30, Chicago Kenwood Academy High School honor student Catherine Smith did what she does nearly everyday. She bought her lunch at a South Side McDonald's a block from her school and sat down in the restaurant to eat it. <p>
This time, the 15-year-old African American was asked by security guards to move to the designated seating area for students. But the seating area was full, she had paid for her lunch, and she intended to eat it. "I paid for the food just like everyone else, and I'm not leaving," she told the guards.<p>
Catherine soon found herself handcuffed by the Chicago Police and sitting in a paddy wagon. Friends called her mother, who asked the police to release her daughter to her, but the police refused. Instead, they took her back to school in the paddy wagon. <p>
She was not formally arrested or charged with any crime--after all, the 1964 Civil Rights Act ended segregation in public accommodations!<p>
Catherine, her family and friends contacted the press and community about the incident. Many were outraged, and the owner of the restaurant apologized and stated that the policy was changed. The following day, about 100 people attended an anti-racist protest in front of the Golden Arches to "support our youth" and demand a public apology from McDonald's management.<p>
With chants of "No justice, no peace," "Supersize justice," and "Rosa Parks of the Golden Arches," students and community members sent their message loud and clear to McDonald's and the rest of the community.<p>

