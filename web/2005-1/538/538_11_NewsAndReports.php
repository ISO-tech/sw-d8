<p><font face="Times New Roman, Times, serif" size="4"><b>No to war and occupation</b></font><p>

<font face="Times New Roman, Times, serif" size="3">MONTPELIER, Vt.--Some 300 antiwar and social justice activists from across Vermont gathered here April 2 to demand an end to the occupation of Iraq and raise awareness of the right wing's attack on the rights of working people.<p>
Jimmy Massey, a founding member of Iraq Veterans Against the War and former Marine recruiter who is currently on a counter-recruitment tour of Vermont and Massachusetts, spoke about how he went from a gung-ho Marine to an antiwar veteran. As a recruiter, "I lied, cheated, and stole to reach my quota for the month," said Massey, and as a Marine in Iraq working on road checkpoints, "I killed innocent people for our government."<p>
Joseph Gainza, one of the main organizers of the march, told the crowd, "We all want Bush out, but it's not enough to get rid of Bush. He is standing on the shoulders of every president we've ever had."<p>
-- At <b>Columbia University </b>in New York City, more than 70 students attended a mid-day March 29 speakout to oppose the occupation of Iraq. The speakout, sponsored by the Columbia Antiwar Coalition, featured speakers from City College's Antiwar Coalition and Stop McCarthyism at Columbia, who connected the occupation with increased levels of domestic repression.  <p>
-- At <b>New York University, </b>a protest called by the Campus Antiwar Network (CAN) at New York University (NYU) successfully forced the CIA to cancel a recruitment event on campus last week. <p>
NYU, along with the University of Texas-Pan American, has developed a relationship with the CIA, part of which includes a marketing class, whose final project is to build an advertising campaign marketing the CIA as an "employer of choice," as well as "dispelling myths associated with the CIA," according to the class's website, www.morethanyouthink.org. "Forcing them to cancel their big speaking event was a huge victory," said CAN member Elizabeth Wrigley-Field. "It showed them they can't market an agency that supports torture and murder around the world without a fight."<p>
-- At <b>Mission High School </b>in San Francisco, about 150 people participated in a teach-in about Iraq last week. Cindy Sheehan, a mother of a U.S. soldier killed in action; Sean O'Neil, Iraq Veterans against the War; Howard Wallace, vice-president of the San Francisco Labor Council; and several others spoke at the event.<p>
<i>Jonah Birch, Abe Gomez, Lucas Keturi and Steve Ramey contributed to this report</i>. 

