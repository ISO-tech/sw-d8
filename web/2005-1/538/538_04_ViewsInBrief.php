<p><a name="Point"></a><font face="Times New Roman, Times, serif" size="4"><b>What "turning point" in Iraq?</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THE JANUARY 30, 2005, election is proclaimed to be the turning point of the war in Iraq. Believing the Bush administration's propaganda about it, Congress asked for a schedule to withdraw the U.S. troops the next day. The administration protested that withdrawal would only broaden the war.<p>
George W. Bush is correct. Most of the voters thought this would speed the departure of the occupying army, and the remainder supported one side of the existing civil war: the side backed by the Americans.<p>
Civil war has never required a majority. Twenty percent of the population kicked off the American Revolution, and historians agree there was never more than one-third actively supporting the victors.<p>
Many turning points have been proclaimed in the Iraq war. The death of Saddam Hussein's sons was applauded as one. It was not. Capturing Saddam Hussein was alleged to be one. It was not. That absurd Constitution of June 2004 was another one. Just read it to discover why it was not. Most recently, leveling Falluja to "save it" was advertised as the biggest turning point of all. <p>
Iraq has cost the U.S. more than $160 billion so far. Over 1.350 U.S. soldiers have died. About 10,000 have been wounded. Less publicized, on January 11, 2005, it was reported that over 5,000 U.S. troops have deserted.<p>
The Iraqi rebels have 40,000 hardcore fighters, supported by 200,000 part-time fighters and volunteers. The U.S. has about 120,000 troops engaged in Iraq now. This one-to-three ratio in active combatants supported by a population core is substantially superior to the ratio of Viet Cong forces to U.S. troop strength throughout the Vietnam War.<p>
There were 1,400 attacks on U.S. troops in September 2004. There were 1,600 attacks on U.S. troops in October 2004. There were 1,950 attacks on U.S. troops in November 2004. It isn't better. It's getting worse. One might believe we are not welcome.<br>
<b>Richard Geffken, </b>Sumter Correctional Institution, Bushnell, Fla.<p>

