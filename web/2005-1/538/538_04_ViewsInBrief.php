<font face="Times New Roman, Times, serif" size="4"><b>Media bow to "right to life"</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ONE OF the most striking aspects of the media's coverage of the Terri Schiavo debacle has been the huge amount of attention they have given the so-called "right-to-life" protesters. <p>
The media's portrayal of these protests has been remarkably even-handed and positive. Protest leaders, such as Operation Rescue's Randall Terry, have been interviewed respectfully and at length; even parents who arranged to have their minor children arrested in front of TV cameras have been depicted as heroic. <p>
Compare this to the media's treatment of recent antiwar demonstrations. The media consistently downplayed antiwar protests, underreporting the numbers of protesters. What coverage the media have given has focused on unrepresentative acts of literal or symbolic violence--scenes of protesters scuffling with police, of black-clad young people smashing windows, of protestors burning American flags, and so on. And antiwar spokespeople are almost never interviewed or asked to comment about anything. <p>
The result is that while "right-to-life" protesters are portrayed as brave citizens standing up for a just cause, antiwar demonstrators are depicted as a roiling, inarticulate mob bent on violence. Can anyone think of a more flagrant example of right-wing media bias? <br>
<b>Dennis Fritz, </b>Laredo, Texas<p>

