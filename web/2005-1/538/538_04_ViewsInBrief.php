<p><a name="Liberty"></a><font face="Times New Roman, Times, serif" size="4"><b>"Liberty" for corporations</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AT THE end of your article about Bush's hypocrisy in addressing the Lebanon issue you stated, "What the U.S. is spreading in the Middle East isn't democracy. It's the ironfisted grip of U.S. imperialism ("Hypocrisy on the march," March 11).<p>
While I certainly agree with this assessment, there is another interpretation I have long held. What the neocons and their figurehead Bush are aiming for is nothing less than worldwide, unregulated, pseudo-free market capitalism. They speak of democracy, but have no intention of allowing it to get in their way.<p>
When Bush speaks of liberty, what he means is the liberty of U.S. corporations to control and profit from all forms of industry in any country they choose--for example, Coke trying to control water rights in parts of Asia. <p>
What gives them the right to even attempt such a thing? As a U.S. citizen, I have never wished for anything in my entire life as much as I wish for this undemocratic, un-American idea to fail--to fail so badly it will never be tried again.<br>
<b>Don Groves,</b> Oregon City, Ore.<p>

