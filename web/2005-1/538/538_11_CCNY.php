<font face="Times New Roman, Times, serif" size="5"><b>Campaign defends rights of CCNY activists</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Yusef Khalil</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 8, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Some 50 people attended a town hall meeting at the City College of New York (CCNY) to defend three students and one staff member punished for protesting military recruitment on campus. The meeting was sponsored by the City Defense Campaign and the Professional Staff Congress (PSC)--the union that represents CCNY faculty and staff.<p>
The purpose of the meeting was to counter the lies that the CCNY administration has been putting out about the arrest of the students, who have been suspended and barred from campus, and the staff member, who was suspended for 30 days without pay. Though President Gregory Williams asserts that demonstrators "grabbed and hit" one of the security officers, several witnesses explain that the security officers were the ones who attacked and brutalized the students.<p>
Elizabeth Weill-Greenberg, a freelance reporter for the <i>Amsterdam News, </i>had been following the case and interviewing students and witnesses. When she found out that the administration's story didn't add up, she tried to convince her editor to let her do further follow-up. Instead, she was fired.<p>
Steve Leberstein, City College chapter chair of the PSC, pointed out these events have made students and faculty afraid to speak their minds, and will result in the university community censoring itself. "Once we begin to do that, it will undermine the very purpose of the university," Leberstein said.<p>
Tiffany Paul from the Campus Antiwar Network gave examples of successful protests across the country that have driven military recruiters out, including two earlier peaceful demonstrations at CCNY itself. And Donna Lieberman from the New York Civil Liberties Union stressed that "the university is acting not as a tool of learning, but of government repression." In punishing the students and staff before their disciplinary hearings, "CCNY is making a mockery of due process," she said.<p>
The disciplinary hearings will be on April 8 for the students and April 14 for Carol Lang. The City Defense Campaign is calling on supporters to pack the hearings in a show of solidarity.<p>

