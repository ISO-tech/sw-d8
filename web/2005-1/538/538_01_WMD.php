<font face="Times New Roman, Times, serif" size="5"><b>WMD report lets Bush off the hook</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 8, 2005 | Page 1</font><p>

<font face="Times New Roman, Times, serif" size="3">A BIPARTISAN presidential commission released its report last week that lays the blame for Iraq's non-existent "weapons of mass destruction" on the CIA and other intelligence agencies. According to the commission, the CIA in particular was "dead wrong" about Iraq's weapons arsenal--its report slams the agency for being overly bureaucratic, having a "culture of enforced consensus" and failing to communicate with other agencies. <p>
But ignored in the commission's findings is the Bush administration's own role in building a case for war on a few easily discredited claims about weapons of mass destruction.<p>
When asked if "the war against Iraq was a waste" at a news briefing last week, panel co-chair Laurence Silberman bluntly replied that the war was a "policy issue" that the commission "didn't deal with." In other words, the report lets the Bush administration off the hook for its pre-war drive to seize on any "intelligence"--no matter how faulty or outlandish--to justify an invasion.<p>
The Bush administration made it clear at every step that it wouldn't take "no" for an answer when it came to intelligence about Iraq's weapons. But what emerges from the report is the conclusion that U.S. intelligence agencies simply didn't do a good enough job spying.<p>
George Bush announced last week that he would support many of the commission's recommendations, including a new counter-proliferation center to coordinate bureaucracy on nuclear, biological and chemical weapons, as well as a reorganization of the Justice Department to enable one office to handle intelligence, counterintelligence and counterterrorism.<p>
This means increased cooperation between organizations like the CIA, National Security Agency and FBI--something that was curtailed in the 1970s following the antiwar movements' exposure of government abuses at home and abroad.<p>
Making intelligence agencies take the fall for the weapons of mass destruction scandal is a win-win situation for the Bush administration. It can now officially claim that it, too, was "misled" about reports of Saddam Hussein's arsenal of nuclear and chemical weapons--while taking the opportunity to reshape the CIA and other agencies into even more secretive and ruthless bodies.<p>
For their part, the Democrats are responding by rehashing their election-year propaganda that the invasion of Iraq was a distraction from the "real threat" from countries like North Korea and Iran. "This is much more than a wake-up call," John Kerry said in a statement last week. "Not only was the intelligence dead wrong about Iraq, but with growing threats in Iran and North Korea, we must take deadly seriously the commission's conclusion that we know disturbingly little about the weapons programs of hostile nations."<p>
Thanks to Kerry and the Democrats, questions about the real reasons for the war on Iraq--the drive for oil and empire--will remain unasked in Washington.<p>
As former weapons inspector-turned-Bush opponent Scott Ritter wrote, "In the end, it is the policymakers--British and American alike--who must shoulder the responsibility for the Iraqi WMD fiasco...The Presidential Commission says that the CIA was 'dead wrong' when it came to assessing Iraqi WMD capabilities, but the fact of the matter is that it is George Bush and Tony Blair who were dead wrong, to the tune of over 1,500 American, nearly 90 British, and tens of thousands of Iraqi lives lost, in pursuing a war on such blatantly false premises."<p>


