<font face="Times New Roman, Times, serif" size="5"><b>The real oil-for-food scandal</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 8, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">ANTHONY ARNOVE edited the book <i>Iraq Under Siege </i>and is coauthor, with Howard Zinn, of <i>Voices of a People's History of the United States. </i>Here, he examines the controversy over the United Nations (UN) oil-for-food program in Iraq, following the release of the findings of a corruption investigation.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">A COMMISSION investigating allegations of corruption in a UN humanitarian assistance program in Iraq during the 1990s cleared Secretary General Kofi Annan of wrongdoing. <p>
Annan had been accused of steering a contract to his son, Kojo, through the oil-for-food program, which allowed Iraq to sell oil on the world market and use money approved by a UN committee for food and other humanitarian goods. But the real scandal never had anything to do with Kojo Annan or kickbacks from certain contracts.<p>
Outrage over the oil-for-food program was concocted as a means of diverting attention from the crisis of the U.S. occupation of Iraq. The U.S. government for years allowed Iraq's illegal oil exports to Jordan, for example, because Washington felt it was important to support a key ally in the Middle East that had been hurt by the financial impact of the 1991 Gulf War.<p>
Meanwhile, $8 billion in money supposedly intended for humanitarian reconstruction projects in Iraq after the 2003 invasion has disappeared. And, according to an investigation by the <i>Financial Times</i>, Iraqi Kurds are now trying to launder 14 tons of cash they received through the oil-for-food program. <p>
"The Coalition Provisional Authority (CPA) had shipped the money to the Kurds in three helicopters filled with shrink-wrapped blocks of 100-dollar notes," the paper reported. "The money, which was outside the regular budget, would have weighed 14 tons and represented the equivalent of around six months regular financing for the Kurdish regional government."<p>
Yet even these scandals, which the media has largely ignored, pale when set against the real crimes of the oil-for-food program, which provided a humanitarian cover for the brutal embargo of Iraq.<p>
Under the oil-for-food program, U.S. and British officials routinely blocked humanitarian supplies to Iraq, claiming they were "dual-use" items, with potential military applications. The UN sanctions committee blocked vaccines, water purification equipment and much more--even pencils.<p>
Meanwhile, as health experts warned, the sanctions led to a sharp increase in infant mortality and deaths from easily preventable water-borne disease. One UNICEF study suggested that the number of extra deaths of Iraqi children under five years of age because of the sanctions was more than 500,000.<p>
That's why two heads of the oil-for-food program--Denis Halliday and Hans von Sponeck--resigned in protest. Halliday called the sanctions a form of genocide when he stepped down to dedicate himself to ending the embargo.<p>
Unfortunately, the embargo only ended with the 2003 invasion of Iraq, which has led to perhaps 100,000 more extra deaths, according to a study published in the British medical journal <i>Lancet.</i> But don't expect any independent commission to investigate these real crimes of the UN--or of the U.S. occupation.<p>

