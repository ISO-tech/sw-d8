<font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Behind the maneuvers in Iraq</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A government made to order</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">April 8, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THE SQUALID maneuvers over Iraq's next government have further exposed the U.S. imperial power play in last January's elections--and highlighted Washington's long-term plans to dominate Iraq through weak puppet governments.<p>
The January 30 vote--held under conditions of martial law--was first demanded by the Shiite Islam parties led by Ayatollah Ali al-Sistani. The massive Shiite turnout reflected the aspirations of the long-oppressed Iraqi majority. <p>
But Washington ensured that the vote would reinforce ethnic and religious sectarian political arrangements first imposed by the occupation's Governing Council. As a result, the opening session of Iraq's new assembly March 29 collapsed into shouting over the failure to name an assembly speaker--a largely ceremonial post. Officials then expelled the media from the room, while the broadcast of the proceedings was replaced with a singer belting out the national anthem.<p>
A deal was cut behind closed doors, and Hajim al-Hassani, a Sunni Muslim Arab, was named to the job April 3. A longtime exile in the U.S., he returned to Iraq behind the U.S. invasion force. Now comes even more intense horse-trading over the most important job--prime minister.<p>
Already, Iraqi politicians admit that they probably won't be able to write a constitution and hold elections--the sole purpose of the new government--until at least six months after their original deadline of the end of this year.<p>
In any case, the real power in Iraq remains the brutal U.S. military machine. Last year, the Pentagon made an example of Falluja by combining Russia's policy in Chechnya of razing whole cities with forcing residents to carry passes--in a manner reminiscent of the Nazi Warsaw Ghetto imprisonment of Jews in the Second World War. Random killing, arbitrary imprisonment and torture by U.S. forces remain routine nearly a year after the Abu Ghraib prison scandal was exposed.<p>
Politically, Washington's agenda is being advanced mainly by the Iraqi Kurdish parties, who've had quasi-independence since the U.S. set up a protectorate following the 1991 Gulf War. They're pressing for control of oil-rich city of Kirkuk, and they've won agreement that Jalal Talabani of the Patriotic Union of Kurdistan will be president of the Iraqi state, giving him--and, effectively, Washington--veto power over Iraqi policy.<p>
Far from "liberating" the historically oppressed Kurds, Washington has harnessed them to its imperial project. The vehicle is the "transitional administrative law" drafted by the U.S. The law requires the assembly to have a two-thirds majority vote on the key posts of president and two vice presidents. These three are to appoint a prime minister, who in turn selects a cabinet. This will blunt, if not negate, the power of the near majority of Shiite parties in the assembly.<p>
"The elections held under the control of the occupying forces in January were neither free nor fair," Ayatollah Jawad al-Khalisi, secretary general of the Iraqi National Foundation Congress, wrote in Britain's <i>Guardian </i>newspaper. <p>
"Instead of being a step towards solving Iraq's problems, they have been used to prolong foreign rule over the Iraqi people," continued Khalisi, whose group spans religious and ethnic groups. He added: "The occupying powers are now following a policy of divide and rule, encouraging sectarian and ethnic divisions and imposing them on all the institutions they have created."<p>
While Washington certainly doesn't want all-out war in Iraq, a permanent conflict will help the U.S. claim justification for maintaining the 14 military bases being built there. Sectarian and ethnic divisions in Iraq also provide a useful argument against calls for the immediate withdrawal of U.S. troops, by claiming that "chaos" would be the result.<p>
Unfortunately, some prominent figures in the antiwar movement have accepted this logic--saying essentially that although the war was wrong, the occupation must remain for now so as not to "abandon" the Iraqi people to "civil war."<p>
In fact, the next move for the U.S. is to entrench ethnic and sectarian divisions under guise the of a federated government structure in a new Iraqi constitution. This is what stokes the conflicts in Iraq and the potential of civil war.  <p>
Every bloody day longer that the U.S. occupation lasts is another day that genuine self-determination for the Iraqi people is denied--and another step towards the U.S. imperial domination of the Middle East. The demand to bring the troops home now is more urgent than ever.<p>

