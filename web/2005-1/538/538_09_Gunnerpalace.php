<font face="Times New Roman, Times, serif" size="5"><b>A much-needed view inside today's Iraq</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Bob Quellos</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 8, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Gunner Palace, </b></i>directed by Petra Epperlein and Michael Tucker.</font><p>

<font face="Times New Roman, Times, serif" size="3">OUR KNOWLEDGE of the real conditions inside Iraq has been limited since the first bombs were dropped on Baghdad more than two years ago. The corporate media's "embedded" reporters have proven utterly unable to produce coverage that differs from the line put out by the Pentagon and the Bush administration. <p>
And unfortunately, independent media inside Iraq are limited and often hard to get access to. The result is a virtual blackout of what is taking place inside the country.<p>
<i>Gunner Palace,</i> the recently released documentary by Michael Tucker and Petra Epperlein, points the cameras inside Iraq to shed light where it is desperately needed. <p>
By sewing together interviews with soldiers from the 2/3 Field Artillery, along with footage of house raids and street patrols through Baghdad, the film helps reveal the reality of the occupation--for both U.S. troops and Iraqi citizens. <p>
The interviews were filmed mostly at a palace built by Saddam Hussein and previously inhabited by his son Uday, who used the premises to throw lavish parties. During the "shock and awe" phase of second invasion of Iraq, the U.S. dropped a bomb on the palace--and has since taken up residence inside.<p>
The compound is still shredded from the damage--except for a putting green installed by the military to accompany the still-existing pool and stocked fishing pond of the old palace. The luxuries led one soldier to call the compound "an adult paradise." But under daily attacks by rocket-propelled grenades (RPGs), it's nothing of the sort.<p>
Still, while conditions at the palace aren't ideal, patrols on the streets of Baghdad are a nightmare. Soldiers ride on the edge of their seats, trying to avoid roadside bombs often hidden in bags or piles of garbage. Convoys stop traffic for long periods of time to dismantle suspected bombs that often prove to be nothing more than a plastic bag. Continuous gunfire and RPGs have become second nature to these soldiers, but roadside bombs are frying their nerves. <p>
The film puts house raids carried out by U.S. forces front and center. Patrols are sent through Baghdad to find suspected resistance fighters. Front doors of homes are knocked in; a dozen troops enter with pointed rifles; and family members plead while arrests are made. The detained are sent to Abu Ghraib prison, even when weapons are not found--and they rarely are. <p>
This is the brutal and uncompromising face of U.S. occupation.<p>
Partially due to the use of handheld cameras, the film's footage has a raw edge. It is accompanied by an equally raw and gritty soundtrack, composed of hip-hop beats often overlaid with vocals from the soldiers stationed in the palace. Their lyrics reveal frustration with the occupation, alongside a sentiment that people at home are forgetting about the war. As one solider rhymes, "For y'all, this is just a show, but we live in this movie."<p>
Interviews with officers are usually filled the message the Pentagon wants to send, but regular soldiers express sentiments that often resemble those heard during Vietnam. As SPC Stuart Wilf says, "If you see any politicians, be sure to let them know that while they're sitting around their dinner tables with their families, talking about how hard the war is on them, we're here, under attack, nearly 24 hours a day, dodging RPGs, and fighting not for a better Iraq, but just to stay alive." <p>
<i>Gunner Palace</i> not only provides a much-needed view inside the occupation of Iraq, it also creates another solid argument for ending the occupation now. <p>


