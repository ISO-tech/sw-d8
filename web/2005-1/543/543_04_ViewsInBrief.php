<p><a name="CAFTA"></a><font face="Times New Roman, Times, serif" size="4"><b>What's wrong with CAFTA?</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AT A demonstration in April, disgruntled workers from various labor unions around Montana showed their lack of support for the proposed Central American Free Trade Agreement (CAFTA). <p>
Labor members from four cities--Helena, Great Falls, Missoula, and Billings--organized protests outside of several Montana field offices in the hopes of convincing Sen. Max Baucus (D-Mont.) that CAFTA would adversely effect U.S. factories and workers. Many of the protesters were armed with signs, voices, and literature denouncing the proposed policies of CAFTA. <p>
Though the logistics of CAFTA have been discussed and accepted, the trade agreement must still receive a congressional vote in order to finalize its implementation. If passed, CAFTA would open trade with six Central American countries--worrying union leaders and workers that a significant loss of jobs here in the U.S. could be in the near future. <p>
Free trade involving capitalistic systems of government will always undercut the rights of workers in order to meet the financial goals of privatized industry. The idea of free trade (as defined by the oppressive governments and private industries that run them) is not one of <i>providing better jobs</i> for those who live in impoverished countries. Instead, it is used as a propaganda tactic to enslave and exploit innocent individuals for the sake of cheap labor and production. <br>
<b>Michael Hudson, </b>Missoula, Mont.<p>

