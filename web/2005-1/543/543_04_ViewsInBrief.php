<p><a name="Opposite"></a><font face="Times New Roman, Times, serif" size="4"><b>Opposite of empowerment</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I WANT to make a plea to everyone who identifies themselves as left wing and pro-women's liberation to get the word "bitch" out of their vocabulary. <p>
This is a word used all too often with little regard to what it means and the effect it has towards women. The word not only literally means female dog, which is bad enough, but it refers negatively about any woman who speaks her mind confidently and firmly. Even the word "bitching" implies complaining and nagging in a feminine context. <p>
This is a sexist word and only discredits women who are not afraid to take control and speak their mind. <p>
On the other side of this argument are "power feminists," who see this word as something they can take back and make their own. One magazine called <i>Bitch, </i>for example, thinks that, "If we take it as a compliment, it loses its power to hurt us." <p>
Nothing is empowering about identifying oneself with such a negative and oppressive word. That's like saying, "Yeah, I'm too pushy, yeah, I'm too loud and obnoxious." It's not defying the word--it's buying into it. <p>
We cannot fight sexism by using a sexist word. We need to challenge it head-on by building a movement, much like the women's liberation movement of the 1960s and '70s that fought for women's control and equality. Like Queen Latifah said, "U-N-I-T-Y, we got to let them know we ain't a bitch or a hoe."<br>
<b>Sarah Levine, </b>San Francisco<p>
