<font face="Times New Roman, Times, serif" size="4"><b>Debate over an Iraqi union</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THERE IS a debate inside the Labor Committee for Peace and Justice in the Bay Area. <p>
As an affiliate of US Labor Against War (USLAW), the Labor Committee is discussing plans to bring a delegation of Iraqi trade unionists to the Bay Area. USLAW intends to bring unionists from three Iraqi unions--the Iraqi Federation of Trade Unions (IFTU), the Federation of Workers Councils and Unions in Iraq, and the Southern Oil Company Union (SOCU).<p>
The Labor Committee for Peace and Justice is organizing money and visa support to bring oil workers from SOCU to meet with trade unionists and oil workers on the West Coast. Union and antiwar activists should welcome this and work within the coalition to organize an event that will draw together all the forces of the antiwar movement. <p>
However, it is a mistake for the Labor Committee and USLAW as a whole to continue to support the IFTU--the only legal union under the U.S.-dominated Iraqi government.<p>
The IFTU has threatened independent workers seeking to organize in other unions and refuses to denounce the occupation. Last year, it helped block an "out now" resolution before the British Labour Party, and it remained silent while the U.S. destroyed Falluja. It is a union whose leadership is actively collaborating with the occupation. <p>
The debate inside the Labor Committee is only beginning. Union members raising disagreements with USLAW support for the IFTU have been shouted down and called "arrogant" and "chauvinistic" for questioning the IFTU. <p>
But it is arrogant and chauvinistic for the U.S. government to be able to decide how and when workers in Iraq can organize. We should not support a union that is helping the U.S. continue the occupation of Iraq.<br>
<b>Adrienne Johnstone, </b>San Francisco<p>
