<font face="Times New Roman, Times, serif" size="5"><b>"I feel like I fought for nothing"</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Sarah Macaraeg</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 13, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Arlington West,</b></i> a documentary by Peter Dudar and Sally Marr, Laughing Tears Productions, <a href="http://www.arlingtonwestfilm.org" target="_blank">www.arlingtonwestfilm.org</a>.</font><p>

<font face="Times New Roman, Times, serif" size="3">IN SANTA Monica, Santa Barbara and Oceanside, Calif., Veterans for Peace has constructed temporary cemeteries in the sand, honoring the soldiers who have died in Iraq. If a cemetery were created to honor the Iraqi dead, it would fill the entire beach. The memorial also serves as a gathering place for military families and veterans of both the Vietnam and Iraq wars, whose interviews are documented in the film <i>Arlington West</i>.<p>
The strength of this film lies in the testimonies of those who have fought or lost family members in the war--ordinary people trying to figure out why they've been forced to sacrifice so much for a war based on lies. They speak to the human cost of the war, the true motives behind it and the realities on the ground in Iraq.<p>
As one veteran says, "I know a lot of us were being lied to because I feel like I fought for nothing. I saw exactly what happened in the war and I hear what they tell everyone and it doesn't match."<p>
Another veteran remarks, "I can tell you from my own experience from being in Iraq, that we're the bad guys, we're invading their territory." The documentary highlights the hypocrisy of the Bush administration's claim that it "supports the troops."<p>
Various soldiers, most of whom cite money for college as their reason for joining the military, speak to the amount of trauma undergone in war, how little they're paid, and the treatment they receive from the Veterans Administration. As a homeless Vietnam veteran describes it, "it's as if they don't expect to see a live veteran." <p>
In its effectiveness at speaking to people who haven't yet been exposed to antiwar politics, <i>Arlington West</i> serves as a powerful tool in building the antiwar movement.<p>


