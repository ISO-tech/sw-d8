<font face="Times New Roman, Times, serif" size="5"><b>Kansas' replay of the Scopes trial</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By David Whitehouse</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 13, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">RIGHT-WING opponents of evolution have regained a majority on the Kansas state school board, and they're determined to force science teachers to bring up challenges to the theory.<p>
The board sponsored three days of anti-evolution hearings last week, where witnesses decried current teaching standards for describing science as the "search for natural explanations." That's because they want to clear the way for supernatural explanations.<p>
The witnesses were advocates of "intelligent design theory"--an updated label for creationism, which claims that complex features of living things couldn't be the product of natural processes. But these "experts" were cagey about alternative explanations, since the U.S. Supreme Court has ruled that teaching about a god that created the world in public schools is an unlawful imposition of religion.<p>
So rather than presenting a rival theory to evolution, witnesses promoted doubts about specific explanations, according to the <i>Wichita Eagle</i>. These doubts are supposed to make students think that there is a real scientific controversy over major points of evolutionary theory--including the idea that natural processes account for the adaptative fit between organisms and their environments. Alan Leshner, head of the American Association for the Advancement of Science (AAAS), told the <i>Eagle</i>, "They're trying to imply that evolution is a controversial concept in science, and that's absolutely not true."<p>
For this reason, mainstream scientists, including the AAAS, boycotted the hearings--which were timed for the 80th anniversary of the Scopes "Monkey Trial," where a Tennessee teacher was tried and convicted for teaching evolution in defiance of state law. Scientists did pack the hearing room, though, and talked extensively with reporters.<p>
The Kansas board last tried to revise classroom standards in 1999, but the anti-evolutionists lost the next election. The conservatives have since recouped a 6 to 4 majority, and the new standards are likely to pass in June. In 2002, Ohio became the first state to mandate teaching "the controversy over evolution."<p>
The board's decision will affect teachers' guides and statewide standardized tests. If enough states change their standards, schools across the country could be affected, since publishers adjust the content of textbooks to capture a nationwide market.<p>


