<font face="Times New Roman, Times, serif" size="5"><b>When Jane Fonda took a stand</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 13, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>F.T.A,</i> </b>directed by Francine Parker, starring Jane Fonda and Donald Sutherland.</font><p>

<font face="Times New Roman, Times, serif" size="3">IN 1972, Jane Fonda, Donald Sutherland and others toured their "Fuck the Army" (F.T.A.) stage show to U.S. military bases in the Pacific Rim. The documentary <i>F.T.A., </i>originally released in 1972, follows that tour.<p>
F.T.A. performances were a vibrant part of the GI coffeehouse movement--the loose network of coffeehouses that had sprung up around U.S. military bases as a way for GIs to plug into the movement in the U.S. against the Vietnam War.<p>
The film crew follows the F.T.A. players as they take their show to Hawaii, Okinawa and the Philippines, performing before enthusiastic crowds of soldiers, sailors and airmen. Sandwiched between interviews with GIs, the stage show takes aim at the military brass with satirical skits, folksy sing-alongs and powerful monologues. The audience responses to the skits are as interesting as the show itself.<p>
In one of the first scenes, Sutherland assumes the persona of a sportscaster, giving a play-by-play account of a battle between the Viet Cong and the U.S. Marines. But the battle ends quickly, as Sutherland explains in horror that U.S. planes drop napalm on the Marines, leading to "penalty flags all over the field."<p>
The film provides a rare glimpse into the revolt from below that ultimately forced the Pentagon to withdraw in defeat from Vietnam. In one skit, GIs cheer a scene in which soldiers refuse a colonel's order to retrieve a stranded Army vehicle from the jungle--and force their commander at gunpoint to communicate their "respectful decline to the colonel's invitation."<p>
Today, it's tragic to watch Jane Fonda's moving speeches against the war and sketches against sexual harassment knowing that she moved on from her radical commitment to social justice to be reborn as America's workout queen in the 1980s. Or that now, she makes her grand return to the big screen playing the wicked mother in <i>Monster-in-Law.<p>
</i>Fonda has spent decades apologizing for her trip to Hanoi--and still right-wing veterans attack her. But if Fonda could only recall the gripping closing monologue of <i>F.T.A.,</i> she would have no trouble answering her critics.<p>
"Remember this well, you people who plan for war," proclaims a somber Sutherland. "Remember this, you patriots, you fierce ones, you spawners of hate, you inventors of slogans...We are men of peace, we are men who work, and we want no quarrel...But if you try to range us one against the other, we will know what to do. If you tell us to make the world safe for democracy, we will take you seriously...<p>
"We will use the guns you forced upon us...to defend our very lives, and the menace to our lives does not lie on the other side of a no-man's land set apart without our consent. It lies within our own boundaries...Put the guns into our hands, and we will use them...Give us the slogans, and we'll turn them into realities...And we will live."<p>
This film is well worth seeking out--try libraries and Web sites featuring rare films from the 1970s--and showing to antiwar audiences today.<p>

