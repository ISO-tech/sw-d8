<font face="Times New Roman, Times, serif" size="4">Nightmare for two families</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Punished for being Muslims</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nagesh Rao</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 13, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE NIGHTMARE for two 16-year-old Muslim high school students from New York City continues.<p>
Last month, the FBI--relying on secret "evidence"--detained the two young women, claiming that they were "an imminent threat to the security of the United States based on evidence that they plan to be suicide bombers." The girls, one Guinean and one Bangladeshi, were taken from their New York City homes at dawn and held in a maximum-security juvenile detention center, hours away in Pennsylvania, where they were not allowed to receive mail from friends. They were allowed only one five-minute phone call from their parents each week.<p>
Recently, one of them, Adama, has been released. But in a bitter twist, Adama's father, Mamadou Bah, will be deported to his native Guinea and separated from his five children.<p>
And the other young woman, whose name has been withheld to protect her identity, will be deported to Bangladesh, along with her entire family.<p>
When the Bangladeshi consulate contacted Homeland Security about the case, they were told that the girl was being held because she was in the country "illegally." In fact, she came to the U.S. at age four with her mother and has lived in Queens ever since. Her parents had filed for political asylum and had no outstanding deportation proceedings against them, according to recent reports.<p>
But the arrest of their daughter has clearly been a harrowing experience. No wonder the girl's father chose deportation, rather than face the ordeal of fighting the Homeland Security bureaucracy.<p>
The case of these two girls exposes the brutality inflicted on innocent Muslims in the U.S. today--the direct result of the federal government's post-September 11 witch-hunt. <p>


