<font face="Times New Roman, Times, serif" size="4">Another $82 billion for war and occupation</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Feeding the war machine</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 13, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">THE POLITICIANS in Washington don't have any trouble coming up with $82 billion--as long as it's for war and occupation.<p>
This week, the Senate is set to approve the latest "emergency" spending package for the U.S. wars in Iraq and Afghanistan. Washington may balk at funding the Medicaid health care program for the poor, but when it comes to murder and mayhem around the globe, money is no object.<p>
Almost all of the money in this bill--$75.9 billion--will go to military operations. That's almost $1 billion more than George W. Bush asked for.<p>
In fact, this is the fifth "emergency" spending package that Congress has taken up since September 11, 2001. The costs for Washington's four-year "war on terror"--from Afghanistan to Iraq and anywhere it sets its sights on around the globe--now come to about $270 billion. And that's <i>beyond </i>the annual Pentagon budget--currently running at about $400 billion per year.<p>
Other "emergency" provisions squirreled away in the bill include $592 million for construction of Washington's embassy in Baghdad--the largest in the world. <p>
The bill goes by the name of "Emergency Supplemental Appropriations Act for Defense, the Global War on Terror and Tsunami Relief." Yet "tsunami relief" for Indian Ocean countries that were devastated by the last December's disaster amounts to just $907 million.<p>
To the bill's name should also be added the words "and the Persecution of Immigrants." Because included in the final version of the legislation are provisions like the "Real ID Act," which will tighten the U.S. government's already restrictive immigration policies.<p>
The bill would allow government officials to demand that asylum seekers provide written "corroboration" of their persecution--<i>from their persecutors</i>. Other anti-immigrant provisions include giving Department of Homeland Security officials unconditional authority to build barriers anywhere along the border, in the name of "national security." The bill also provides $635 million--almost as much as will go to tsunami relief--to hire border patrol agents and immigration and customs investigators, and to build more detention facilities.<p>
The Real ID Act would impose strict federal rules on how states issue driver's licenses, requiring that they verify whether applicants are in the U.S. legally. This would bring the U.S. closer to having a "show us your papers" national ID system. "It literally sets up the backbone for a system to track all Americans throughout their lives: their movements, where they're going to and where they're coming from," said Tim Sparapani of the American Civil Liberties Union.<p>
The Republicans in Congress justify this in the name of the "war on terror." "This legislation will tighten our asylum system, which has been abused by terrorists," Real ID sponsor Rep. James Sensenbrenner (R-Wis.) said in a statement earlier this year. "We can no longer give benefits to those aliens who concoct bogus political asylum stories."<p>
In reality, the U.S. approved less than a third of the 41,000 applications for asylum received in 2003, according to U.S. Citizenship and Immigration Services statistics. Now the proposed legislation would put immigrants--particularly Arabs and Muslims, who have already been the target of constant harassment and abuse since September 11--further in the line of fire.<p>
And there's bipartisan support for doing so. The House passed the final version of the spending bill, which included the Real ID Act, easily--in a 368-58 vote. One hundred forty-three of those voting in favor were Democrats.<p>
And while Senate Democrats complained about some of the worst anti-immigrant provisions in the bill, they will probably pass it in the next round of voting. When reporters asked last month whether there was a chance that the Real ID Act might not make it into the larger spending bill, Senate Democratic leader Harry Reid said, "It's only a question of when. A senator came into my office and said, 'I want to filibuster this,' and I said, 'Get real.'"<p>
This is the kind of "realistic" response from Democrats to Republican attacks that we've grown to expect.<p>
Yet even as Bush ramps up war spending with the Democrats' help, support for the occupation is falling further. According to a <i>USA Today</i>/CNN/Gallup poll released May 3, support for the war in Iraq has fallen to its lowest level since the U.S. invaded in 2003. Just 41 percent polled said the war was worth it, and 57 percent said no.<p>
The potential to build on this opposition is there. That's why we have to make our demand loud and clear to both war parties: Not a penny more for war and occupation.<p>


