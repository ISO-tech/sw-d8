<font face="Times New Roman, Times, serif" size="4">Two soldiers arrested for running guns to death squads</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>U.S. fingerprints on dirty war in Colombia</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 13, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE ARREST of two U.S. soldiers in Colombia charged with selling weapons to right-wing paramilitaries has once again highlighted Washington's deep involvement in that country's dirty war.<p>
The two soldiers, Lt. Col. Allan Norman Tanquary and Sgt. Jesus Hernandez of the U.S. Army, were arrested by Colombia authorities just weeks after five other U.S. soldiers were arrested for smuggling cocaine into the U.S. The soldiers are part of the 800 military advisers--increased last year from 600--at the core of Plan Colombia, the U.S. government's multi-billion-dollar effort to militarize the Andes under the guise of the "war on drugs."<p>
Colombian President &Aacute;lvaro Uribe came under severe criticism for handing over the soldiers to the U.S. for prosecution. Under an agreement with Washington, U.S. soldiers can't be prosecuted in Colombia.<p>
Uribe is the U.S. government's chief right-wing strongman in Latin America amid a growing number of center-left governments. In particular, the U.S. wants Colombia--the third-largest recipient of U.S. military aid after Israel and Egypt--to serve as a counterweight to Venezuela, which has swung to the left under President Hugo Ch&aacute;vez.<p>
Now, the arrest of the U.S. soldiers on gunrunning charges has complicated Uribe's efforts to repackage the government's dirty war against left-wing rebels by offering amnesty to right-wing death squads, which back drug lords and collaborate with Colombian armed forces against two leftist guerrilla armies.<p>
Uribe is attempting to ease aside the death squads in favor of a more lethal force--the regular Colombian army, lavishly equipped and well trained by the U.S. military. But there's little distinction between operations by death squads and the military. In February, a unit of the Colombian army massacred seven civilians at the Peace Community of San Jos&eacute; de Apartado.<p>
What's more, according to Colombian investigative journalist Alfredo Molano, the dirty war is now bound up with the government legalizing the fortunes of some drug cartels, even as it prosecutes others. "Uribe is working in support of a war of revenge by the Medell&iacute;n cartel against the Cali cartel," said Molano, whose book on the victims of the dirty war, <i>Desterrados, </i>has just been published in the U.S.<p>
The U.S. will try to ride out the storm over the soldiers' arrests, hoping that their possible prosecution for smuggling arms won't threaten a much larger official gunrunning effort--which includes state-of-the-art helicopters and a vast array of high-tech equipment. Last year, the Colombian military deployed that gear especially in the oil-rich Putamayo region, an area long coveted for development by transnational companies.<p>
However, Uribe faces a growing opposition in the upcoming presidential elections for his economic and political policies. The antiwar movement in the U.S. needs to add to that pressure by demanding an end to U.S. support for his death-squad government. <p>


