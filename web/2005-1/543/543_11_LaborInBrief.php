<p><a name="Franklin"></a><font face="Times New Roman, Times, serif" size="4"><b>Franklin Medical Center</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Wayne Standley</b></font><p>

<font face="Times New Roman, Times, serif" size="3">GREENFIELD, Mass.--"All we want is to be treated fairly." Registered nurses repeated this during their informational picket at Franklin Medical Center (FMC) May 2.  <p>
The picket line stretched for an entire block in front of the hospital, and in a show of solidarity, the nurses, members of the Massachusetts Nurses Association, were joined by members of Service Employees International Union Local 509, United Auto Workers Local 2232, United Electrical Local 274, firefighters and others from the community.<p>
After more than five months of negotiations, management is taking a tough stance. The hospital has offered between 4 and 5 percent annual raises over a three-year contract, but only if nurses accept increases in their health insurance payments--which would more than wipe out their wage increases. Management is also seeking a two-tier contract--in an attempt to divide and weaken the nurses' union--by imposing even steeper increases on those who work less than 30 hours per week.<p>
FMC is part of Baystate Health Systems, one of the largest health care corporations in New England, so it can well afford to provide its workers with good wages and health benefits. With continuing solidarity and unified actions, the nurses can fight off these concessionary demands.<p>
<i>To support the nurses, call Mark Tolosky, CEO of Baystate Health Systems, at 413-794-0000 and demand a contract without concessions</i>.<p>
