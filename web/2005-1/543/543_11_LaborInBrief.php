<p><a name="Seattle"></a><font face="Times New Roman, Times, serif" size="4"><b>Seattle carpenters</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Steve Leigh</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--One hundred carpenters and supporters gathered April 30 to protest the use of nonunion labor on construction sites in the south Lake Union area.<p>
Paul Allen's Vulcan Corporation is re-developing the area with substantial financial aid from the city of Seattle. While some of the financing actually comes from the carpenters' own pension fund, some of the contractors that Allen--one of the world's 10 richest men--is hiring are nonunion, and their workers receive a much lower wage scale and no benefits. They also have less experience and training.<p>
The floor on one building is "already sagging because they forgot to weld the steel reinforcements before pouring the concrete...I'm worried about the safety of anyone who uses that building" said one carpenter, as workers marched from site to site. "You have to stand up, or they'll knock you down," said another carpenter. "Our conditions are fragile, we have to defend them."<p>
This rally was the start of an ongoing campaign to force Vulcan to raise conditions and wages for workers in the area. Other unions were represented, but in very small numbers. A victory for the carpenters would help all workers. Other unions need to actively support this campaign.<p>

