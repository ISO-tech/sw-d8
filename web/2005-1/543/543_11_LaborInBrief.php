<font face="Times New Roman, Times, serif" size="4"><b>San Francisco hotel workers</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Marcia Thorndike</b></font> <p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Thirty-seven people were arrested May 3 here when hotel workers staged a 500-strong march and sit-in at the downtown Hilton.<p>
They aimed to turn up the heat on the 14-hotel Multi-Employer Group (MEG), which has not met with their union, UNITE HERE Local 2, since February 14. The two sides are currently in a "cooling-off period," brokered by San Francisco Mayor Gavin Newsom.<p>
A two-week strike last October that targeted four hotels was followed by a six-week lockout at all 14 of the city's most prominent hotels. When contracts for 4,300 unionized hotel workers expired in August, 2004, workers mobilized to fight for health care, wages, pensions and a contract expiration date of 2006, to bring San Francisco into alignment with other major U.S. cities. <p>
The hotel workers' union, UNITE HERE Local 2, has called for a boycott of the 14 hotels in the MEG, which has resulted in several large conferences being relocated. This financial hit to the hotels is rumored to be causing some of the hotels to want to settle.<p>
While there are no informational pickets to promote the boycott, UNITE HERE Local 2 Vice-President Lamoin Werlein-Ja&eacute;n announced that, "In the upcoming weeks, there will be more demonstrations, more protests and more action inside hotels until they realize that their stalling is not going to dilute the determination to fight for a fair contract." It's important to keep mobilizing and not allow the fightback of last year to fizzle out.<p>
