<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Lenin, Trotsky and internationalism</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 13, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">WHEN THE Russian Revolution triumphed in 1917, Lenin, Trotsky and others were clear that economic conditions were ripe for socialism on a <i>world </i>scale, but an isolated Russia could not possibly survive as a workers' state. "We always staked our play," wrote Lenin, "upon an international revolution...In one country it is impossible to accomplish such a work as a socialist revolution."<p>
When the Stalinist bureaucracy arose beginning in the early 1920s, Trotsky, who had been the key organizer of the 1917 insurrection and who had led the Red Army to victory in the Civil War, became the champion of the fight against Stalin. Before his death in 1924, Lenin had begun to challenge the rising bureaucracy, which included a proposal (suppressed by the central committee after his death) to remove Stalin from his position as General Secretary of the party.<p>
Trotsky denounced Stalin's regime as the "first stage of bourgeois restoration." Citing Marx, Trotsky argued that building "socialism in one country" was not building socialism at all, and that attempting to do so in conditions of scarcity, was merely leading to the revival of relations of social and economic inequality in Russia.<p>
He called for the overthrow of the new bureaucracy--a political revolution. A new epithet was born--"Trotskyist"--and state propaganda was issued to "prove" that Trotsky was, after all, a latecomer to the Bolshevik Party and someone who had really worked to undermine the revolution all along.<p>
Trotsky was a fitting leader of the new left opposition in Russia. His theory of permanent revolution had stipulated that the "combined" character of Russia's economic development--grafting the most modern industry on top of the most backward peasant economy--would create a revolutionary movement that would combine the tasks of a bourgeois and a working-class revolution.<p>
The Russian liberals would unite with the landlord reaction against the working class. "The bolder the struggle of the masses," wrote Trotsky, "the quicker the reactionary transformation of liberalism." Only the working class of Russia could lead a "bourgeois" revolution. <p>
This is more or less what took place in 1917. Yet, like Lenin, Trotsky's perspective was entirely internationalist. The Russian Revolution could not succeed ultimately unless the revolution spread. Without the direct governmental support of the European proletariat," he had written in 1906, "the working class of Russia will not be able to maintain itself in power."<p>
Trotsky had expected a possible defeat to come via the physical destruction of the workers' state from foreign military intervention in conjunction with counterrevolutionary armies. But the Bolsheviks triumphed in the Civil War. The revolution degenerated from within rather than being overthrown. Yet, nevertheless, the isolation of the revolution, as Trotsky had predicted, did indeed lead to workers losing power in Russia--to a bureaucratic counterrevolution.<p>
Many writers have commented on the brilliance of Trotsky and the dullness of Stalin. So what explains Stalin's triumph and Trotsky's defeat? Trotsky's strength as a leader had been strongly associated with the workers' movement when it was at its height, whereas Stalin and the bureaucracy drew their strength from the weakness of the working class.<p>
However brilliant Trotsky's sharp criticisms and analysis of the revolution's degeneration, therefore, conditions favored Stalin's success. Trotsky was finally exiled from the USSR in 1928. For the next decade, Stalin pushed through a complete counterrevolution, obliterating all traces of 1917.<p>
He did this not simply by promoting essentially anti-Marxist ideas under the guise of Marxism--"socialism in one country," a completely bureaucratic, rather than democratic, conception of organization. He also physically liquidated the leaders of the revolution.<p>
According to Soviet Leader Krushchev, Stalin murdered 70 percent of the members and candidates of the party central committee elected in 1934, and 80 percent of them had joined the party before 1921.<p>
Trotsky's fight against the international impact of Stalinism will be our next topic.

