<font face="Times New Roman, Times, serif" size="5"><b>Boston boots the Nazis</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sriram Ananthanarayanan</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 13, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">BOSTON--Approximately 300 counterprotesters forced "White Revolution," a neo-Nazi group with active chapters in 16 states, to tuck tail and leave under police protection on May 8.<p>
White Revolution--a racist, anti-Semitic, antigay, anti-immigrant, white supremacist group--claims that the Holocaust never happened. Several weeks ago, the group announced that it would rally at Faneuil Hall, near the Holocaust Memorial, to protest a Holocaust memorial service.<p>
But a counterdemonstration, organized by the Stop the Nazis Now Coalition, greatly outnumbered the paltry 15-20 Nazis, who were cordoned off and protected by the city police.<p>
The Stop the Nazis Now Coalition arrived early, chanting and marching in the area that the racists were originally hoping to rally at. Over the next two hours, small groups of White Revolution sympathizers that attempted to gather were chased away by the counter-protesters. When the neo-Nazis later started marching with their racist placards, they were greeted by the front line of activists in the Stop the Nazis Now Coalition.<p>
A few coalition members were brutally beaten by cops in riot gear and on horses in the time that it took for the rest of the antiracist forces to join them. But the Nazis were stopped in their tracks after barely taking a few steps--before they even had a chance to get near Faneuil Hall. <p>
Heavily armed police beat back coalition members, allowing the white supremacists to march a few more steps down the street. The neo-Nazis finally settled behind a barricade, protected by two rows of riot police, where they were shouted down for almost an hour by the counterprotesters before ultimately leaving without having been allowed to make a single speech or chant.<p>
While the counterdemonstration itself was a resounding success, there were a number of questions raised during the planning meetings, such as whether or not such a huge demonstration was giving the neo-Nazis undue media attention, or whether they deserved the right to free speech.<p>
But fascism grows precisely when it goes unopposed. Stopping the Nazis from getting a hearing by organizing mass counter-mobilizations is the only way to stop their ideas from spreading.<p>


