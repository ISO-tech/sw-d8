<font face="Times New Roman, Times, serif" size="4">Unions threaten strike over maneuver in bankruptcy court</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>United tries to void contracts</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 13, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">STRIKE THREATS are hanging over United Airlines as the company tries to use bankruptcy proceedings to dump pension plans and void union contracts.<p>
In a hearing set for May 10, United plans to ask a federal judge's approval to hand over pension plans to the government's Pension Benefit Guaranty Corp. (PBGC), saving the company $4.4 billion while workers' benefits are slashed. In another hearing scheduled for the following day, the company will ask the court to void labor contracts altogether and allow the company to impose $725 million in concessions--a move that prompted strike threats from unions representing flight attendants, baggage handlers and mechanics.<p>
United, however, claims that the workers aren't legally able to strike under the Railway Labor Act, which prohibits workers in the railroad and airline industries from walking off the job unless a government labor board rules that bargaining has reached an impasse. If approved, this move would allow management to impose its will while banning the unions from taking recourse.<p>
This would set a precedent for airlines already in bankruptcy to do likewise, such as US Airways. It would also accelerate the demands for greater concessions at other companies, as airline managers slash jobs and wages in a drive to force one or another carrier out of business.<p>
"The bottom line is that people don't trust this company," said Richard Turk, spokesperson for Aircraft Mechanics Fraternal Association (AMFA) Local 9, which represents United mechanics in San Francisco. "The fact is, we've given up many things prior to this. In 2003, the company said, 'This is all we need to get out of bankruptcy.'" Since then, United workers have taken concessions worth $2.5 billion per year with devastating consequences.<p>
The <i>San Francisco Chronicle </i>recently published an article on the fate of United retirees. One, Richard Lesnick, lost virtually his entire $80,000 in company stock in the company's employee stock ownership program when the company faced bankruptcy. Now he faces a 25 percent cut in pension benefits.<p>
Mechanics have seen concessions, and layoffs have cut their numbers at United in half, to just 6,000--a retreat that led mechanics to vote out the International Association of Machinists (IAM) and join AMFA. United's attempt to void the mechanics' contract prompted the IAM, which still represents 20,000 ground workers, to seek strike authorization. The Association of Flight Attendants did likewise.<p>
Following temporary wage cuts already imposed by a judge, United mechanics' pay is about $26 per hour, compared with about $38 per hour at Southwest Airlines, which is touted for its low labor costs.<p>
There's no telling when the judge will make a ruling on United's attempt to void contracts. In the meantime, negotiations continue, with management trying to get unions to buckle under the pressure.<p>
Turk said that AMFA members take seriously management's threats to liquidate the company in the event of a strike. "United probably can't survive without us there," he said. "But there is such a lack of trust, and they feel they've given up enough.  What do we do, go down to $2 per hour? These guys are very angry."<p>


