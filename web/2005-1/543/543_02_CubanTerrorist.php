<font face="Times New Roman, Times, serif" size="5"><b>A terrorist Bush approves of</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 13, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">AFTER THE September 11 attacks, George W. Bush justified the invasion of Afghanistan by declaring, "If you harbor terrorists, you are a terrorist." But wait and see if Bush alienates his right-wing Cuban backers by denying political asylum to Luis Posada Carriles.<p>
Posada, a notorious thug in the anti-Castro movement in Cuba, snuck into Florida six weeks ago and remains in hiding as his lawyer pleads with U.S. officials to grant him asylum.<p>
Posada is a terrorist in every sense of the word. For more than four decades, he fought on behalf of the CIA's Cold War crusade against Fidel Castro--starting with the failed U.S.-backed Bay of Pigs invasion in 1961. Even U.S. officials recognize that Posada played a central role in the 1976 bombing of a Cuban commercial airliner that killed 73 people. <p>
During the 1980s, he worked with the paramilitary death squads in El Salvador in the country's civil war that pitted the U.S.-allied government against left-wing rebels. He has bragged about bombing tourist spots in Havana in the 1990s--one of which killed an Italian visitor in 1997. In Panama, a court convicted him of a bomb plot in 2000 that targeted Castro.<p>
As the <i>New York Times </i>reported, "A grant of asylum could invite charges that the Bush administration is compromising its principle that no nation should harbor suspected terrorists."<p>
In truth, the U.S. has long trained and then granted asylum to CIA "assets" who carried out terrorism and human rights abuses in the Caribbean and Latin America. As Livio Di Celmo, the brother of Posada's Italian victim, said at an April news conference, "The double standard that your government applies on the issue of terrorism is an insult to the memory of all innocent victims of U.S. policies."<p>


