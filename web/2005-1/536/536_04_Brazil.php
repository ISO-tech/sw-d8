<font face="Times New Roman, Times, serif" size="5"><b>The battle for Brazil's poor</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 25, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">AN AMERICAN nun, Dorothy Stang, who had lived in Brazil for 30 years and was a naturalized Brazilian citizen, was recently gunned down on a remote dirt road in the Amazon region where she lived and worked for 23 years.<p>
Brazil's state of Para is a rugged and lawless region where loggers and ranchers are fighting an ongoing battle with the region's poor and environmentalists, who seek a livelihood and to sustain the rain forests. When she was killed by two gunmen allegedly hired by rancher Vitalmiro Moura, Stang was trying to halt illegal logging in an area that Moura coveted. <p>
The assassination of Stang may have backfired on its perpetrators. Brazil's president Luiz In&aacute;cio "Lula" da Silva has proclaimed the 22,239-acre area that Stang was trying to protect for the poor to be reserved for them. The two gunmen that Moura is accused of hiring were allegedly given $19,300 for her murder. One news report said that a consortium of loggers and ranchers had paid for the murder. <p>
Ironically, the move to silence her has made Dorothy Stang a national hero. She will join the ranks of exemplars of decency like Chico Mendes, the martyred Brazilian rubber tapper, and Paulo Freire, renowned educator of the poor and social activist.<p>
An additional problem persists in Brazil that receives little media attention. Sex slavery is a major issue, with thousands of women and girls reportedly forced into it. Brazil ranks second behind Thailand in the numbers of children exploited under sex slavery according to the United Nations.<p>
Brazil faces an ongoing struggle with poverty, the rights of women and children, and the environment. Yet many believe that Lula is moving to the right, making concessions to the moneyed interests and splitting his Workers Party. <p>
If he continues moving to the right, many feel it will be a betrayal to those who put him in power--and to the memory and struggle of Dorothy Stang.<br>
<b>Brian McAfee, </b>from the Internet<p>

