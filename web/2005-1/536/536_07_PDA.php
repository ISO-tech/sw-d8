<font face="Times New Roman, Times, serif" size="5"><b>Can the PDA move the Democrats' left?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 25, 2005 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">IN A recent fundraising appeal on behalf of Progressive Democrats of America (PDA), Global Exchange and Code Pink co-founder Medea Benjamin urged support for the PDA's effort to "take over and transform the Democratic Party."<p>
But this is only the latest in a long line of attempts to "take over and transform the Democratic Party." If history is any guide, the PDA's attempt will end like all the others--in failure.<p>
Perhaps the closest a movement has come to transforming the Democratic Party came in the 1930s with the eruption of the industrial union movement in the Congress of Industrial Organizations (CIO). The Democrats were revived as an electoral vehicle and a tool for capitalist rule as the Roosevelt administration, in the depths of the Great Depression, devised a program to save the system.<p>
The new labor movement quite quickly became an appendage of a pro-business party--one that helped get out the working-class vote while burying or watering down working-class demands in the interests of "party unity." Until the civil rights movement, that meant unity with the right-wing Dixiecrat rulers of the U.S. South, who hated organized labor almost as much as civil rights for Blacks.<p>
This logic affected almost all the main labor leaders of the era--including United Auto Workers (UAW) President Walter Reuther, who once confessed that the UAW could have taken over the Michigan Democratic Party, but refrained from doing so because it wanted to keep the party's middle-class and business supporters. So for years, labor remained the Democrats' most loyal backers, but got little of its agenda--from national health care to repeal of the anti-union Taft-Hartley Act--considered.<p>
No one can realistically compare today's PDA with the CIO of the 1930s and '40s. But that's precisely the point. If the most powerful working-class movement in U.S. history couldn't transform the Democratic Party, how can a few thousand liberal activists--whose preferred 2004 presidential candidates (Dennis Kucinich and Howard Dean) couldn't win a Democratic primary--hope to?<p>
So far, the PDA appears to be just another progressive pressure group inside the Democratic Party, promoting the legislation of 40 to 50 liberal members of Congress. If it becomes a more serious player in Democratic politics, it will face pressure to stand with party leaders who oppose it on issues like the Iraq war, in the interests of "party unity."<p>
An example of just how this works took place recently in the Internet activist network and fundraising machine MoveOn.org. Even though MoveOn gained its prominence by its identification with opposition to Bush's war on Iraq, it recently decided to shelve opposition to the occupation--in favor of promoting the Democratic Party's opposition to Bush's Social Security "reform" instead.<p>
"We're seeing a broad difference of opinion among our members on how quickly the U.S. should get out of Iraq," MoveOn executive director Eli Pariser told columnist Norman Solomon. "As a grassroots-directed organization, we won't be taking any position which a large portion of our members disagree with."<p>
This explanation doesn't pass the laugh test. Like many other liberal groups, MoveOn.org is a staff-driven lobby whose "members" have no control over the organization's direction. While it's no doubt true that there is a range of opinions about Iraq among MoveOn contributors and "Internet activists," the staff's interpretations of those opinions is what counts. So Pariser could easily have argued the opposite case--in favor of ending the occupation "as soon as possible," or some other nebulous formula--because that position would no doubt reflect a substantial opinion among MoveOn constituents as well.<p>
Why "move on" to Social Security instead? Most likely because the Democratic politicians that MoveOn plays to oppose getting out of Iraq. Meanwhile, they are finding that opposition to Bush's Social Security privatization scheme has actually earned them support.<p>
Because MoveOn wants to be taken seriously as a "player" in Washington and because it is, at heart, a partisan pro-Democratic organization, it is deferring to its "friends" in the Democratic Party. This is how "grassroots" organizations that talk about influencing the Democratic Party end up being influenced by it instead.<p>


