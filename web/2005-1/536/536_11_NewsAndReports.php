<font face="Times New Roman, Times, serif" size="4"><b>No to war and occupation</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--About 150 people made their way to two different protests March 16 at Hunter College in Manhattan.<p>
The first action was called by the Campus Antiwar Network to protest military recruitment at the campus career fair. Protesters confronted recruiters with posters of atrocities at Abu Ghraib, as well as asking them about the discriminatory "don't ask, don't tell" policy. A National Guard recruiter told protesters, "I want to find a way out. I don't agree with what we're doing in Iraq, and I'm with you."<p>
The second protest, immediately following the anti-recruitment action, targeted the proposed tuition hikes and budget cuts taking place at the City University of New York.<p>
In <b>Pasadena, Calif., </b>Iraq war veteran and conscientious objector Aidan Delgado spoke to a crowd of more than 200 students at a public forum about the costs of the war in Iraq for both working-class people in the U.S. as well as the Iraqi people. The event, sponsored by Students for Social Justice at Pasadena City College, was part of an emerging student movement at community colleges and high schools around the country to counter the increased militarization of poor working-class schools.<p>
Delgado showed the crowd pictures that he had taken during his two years in Iraq, including graphic pictures of Iraqis that had been killed by the U.S. military--both civilians and armed resisters.<p>
Two days later, Students for Social Justice held a speak-out against the war in Iraq and military recruiters in the school quad.<p>
In <b>Providence, R.I., </b>Iraq Veterans Against the War member Patrick Resta spoke to 30 Rhode Island residents March 11. Resta described shocking neglect of soldiers in relation to their inadequate food, housing, medical care and lack of protective armament.<p>
<i>Freddy Garcia, Peter LoRe and Bill Pagliarulo contributed to this report.</i> <p>

