<p><font face="Times New Roman, Times, serif" size="4"><b>Gay marriage is a right</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jocelyn Blake</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN DIEGO--About 70 activists and supporters of marriage equality held a rally March 14 in support of a California Superior Court ruling that the exclusion of same-sex couples from marriage is unconstitutional. The rally, organized on only a few hours' notice, was called by Equality California (EQCA), a statewide, grassroots-based organization for lesbian, gay, bisexual and transgender (LGBT) rights.<p>
By rejecting the notion of "separate, but equal," the court ruling is a huge step forward in California's struggle to gain equal marriage rights.<p>
Activists at the rally explained to the crowd that the ruling is just one victory in a long struggle for equality and that we have to keep up the pressure. "We take [the decision] as a huge victory, but we recognize that we have a long road ahead of us toward justice," said Nadine Jernewall, head of EQCA's San Diego chapter.<p>
LGBT rights activists expect a backlash from their antigay opponents in response to the ruling--including a push for a constitutional amendment against same-sex marriages. "Such an amendment would not only exclude LGBTs from marriage, but it would take away domestic partnerships," Jernewall said. "It's clear that [the amendment backers'] main concern is not marriage and family, as they claim, but rather a reflection of their homophobic, antigay agenda."<p>
Only by continuing to demand equality will such efforts to maintain institutionalized discrimination be curtailed.<p>
