<font face="Times New Roman, Times, serif" size="5"><b>Evil is lurking...in cartoons?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Cindy Beringer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 25, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">WARNING! THE radical right has identified the latest dangers to the children--a precocious cartoon bunny and an animated sea sponge. The neo-Puritans' red alert suggests that, left unchecked, the antics of these two graphic representations could lead to the spread of dreaded tolerance throughout the land and the end of hatred as we know it.<p>
<i>Postcards from Buster</i> is a PBS series partially funded by the Department of Education and the No Child Left Behind Act for the purpose of teaching diversity. The animated Buster, whose animated parents are divorced, travels the country with Dad while intermingling with real children in various situations. The clever bunny has introduced viewers to real kids climbing rocks in Colorado, clogging in Kentucky and rodeo barrel racing in Texas.<p>
New Education Secretary Margaret Spellings has her bowels in an uproar over the "Sugartime!" episode, which includes a less-than-one-minute encounter with lesbian parents. While learning how maple sugar and cheese is made in Vermont, Buster's young friend Emma shows him her favorite family photo of two women standing cheek to cheek. "It has my mom and Gillian, the people I love a lot, and they mean a lot to me."<p>
Spellings is another Bush cabinet appointee from Texas, where health textbooks are required to define marriage as a lifelong commitment between a man and a woman.<p>
Spellings doesn't hesitate to leave Emma behind. Within two days of taking office, Spellings threatened to cut funding for the series and dis-invited the show's executive producer from speaking at a children's TV conference. "Many parents would not want their young children exposed to the lifestyles portrayed in this episode," she said.<p>
PBS immediately decided not to distribute the series to local stations, but about 200 of the 350 stations have elected to air the show anyway in a hodgepodge of compromises. KLRU in Austin, Texas, decided to air the show in response to overwhelming public demand, but moved the show from its 3 p.m. slot to 8 p.m. with ample parental warnings--lest a child should stumble upon such dangerous subject matter.<p>
Then, at an inauguration week banquet, Dr. James Dobson of Focus on the Family warned them about SpongeBob SquarePants' hidden agenda to teach "homosexual propaganda to children."  Wildly popular, SpongeBob appears several times daily on Nickelodeon, stars in a movie, and apparently collects no royalties on a mess of merchandise bearing his image. <p>
Dobson was attacked by the right and the left for his censure of this free-market success story, prompting him to post a seven-page defense on his Web site. A video produced by the We Are Family Foundation, which is scheduled for distribution in private and public elementary schools in March and features SpongeBob, is the focus of his ire. "The video itself is innocent enough," he admits, "and does not mention anything overtly sexual."<p>
His evidence of the "sinister agenda" behind the video--a statement on the We Are Family Foundation Web site that includes sexual identity as one of several areas for tolerance. Dobson ends his message with special thanks to Spellings for her shots at the friendly bunny. He also includes a list of biblically related parental duties guaranteed to produce repressed and miserable children.<p>
As Spellings and Dobson compete with the cartoons with their slapstick behavior, the real dangers facing our children are allowed to fester--hunger, inadequate medical care, military recruiters lurking in our schools, and oh yes, bigotry.<p>


