<font face="Times New Roman, Times, serif" size="5"><b>The antiwar TV show you might have missed</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Mike Corwin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 25, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>American Dreams, </b></i>airs Wednesday nights on NBC. Season 1 out on DVD.</font><p>

<font face="Times New Roman, Times, serif" size="3">SOMEHOW BELOW the radar of the cultural warriors of the right, a remarkable antiwar show appears weekly on network television. NBC's <i>American Dreams, </i>now in its third season, depicts the lives of the Pryors, a Catholic family in mid-1960's Philadelphia.<p>
Daughter Meg spends her days in high school and dancing on the music show "American Bandstand." This potentially annoying story device allows <i>American Dreams </i>to showcase some terrific music from the 1960s--and appearances by contemporary artists like Usher and Alicia Keys who recreate some classic pop performances.<p>
But the show is most notable for incorporating the political climate of the 1960s into its stories. Racism and civil rights, homophobia, the sexual revolution, the student radicalization and Black nationalism have found their way into <i>American Dreams' </i>stories over the years.<p>
But the heart of this season has been the Vietnam War and its effects on the Pryors. The show depicts how the family members' ideas are changing and being shaped by events unfolding in the world.<p>
Suppression of political speech in Meg's Catholic high school compels her and her friends to organize dramatic antiwar protests. Conservative mom Helen joins a peace group in her church and secretly assists a draft resister's escape to Canada.<p>
Meg's brother J.J. is a Marine wounded in Vietnam. He opposes his family's antiwar activism, but is haunted by his experience and is questioning his role as a military recruiter.<p>
This being GE-owned network TV, I'm prepared to be disappointed if <i>American Dreams </i>starts to bury the political themes in favor of traditional family drama. But that hasn't happened yet, and with rumors of the show's cancellation, who knows where these stories will go. Will J.J. finally come over to the antiwar movement? Catch this noteworthy show while you still can.<p>

