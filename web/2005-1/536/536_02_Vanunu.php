<font face="Times New Roman, Times, serif" size="5"><b>Israeli dissident faces new jail term</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 25, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE COURAGEOUS Israeli dissident Mordechai Vanunu is facing another term in prison for violating the terms of his release last April after an 18-year jail sentence.<p>
Vanunu was imprisoned in 1986 for revealing that Israel had a secret nuclear weapons arsenal consisting of several hundred warheads. For most of his nearly two decades behind bars, Vanunu was kept in solitary confinement, locked away for 22 hours a day in a 6-by-9-foot cell.<p>
When he was released, an Israeli court barred Vanunu from contacting or passing information to foreign journalists, or from leaving Israel. Vanunu said from the start that he wouldn't accept the gag order, and began giving interviews to international media outlets.<p>
Now, Israeli prosecutors have filed 22 charges against him for allegedly exposing nuclear secrets in interviews--and for attempting to visit Bethlehem in the Occupied Territories for a midnight mass on Christmas Eve. He could go to prison for two more years if convicted.<p>
The claim that Vanunu has revealed new nuclear secrets is absurd. He revealed everything he knew about Israel's nuclear program 20 years ago. "I have given away no sensitive secrets," Vanunu told Britain's <i>Guardian </i>newspaper, "because I have none."<p>
But Vanunu is a constant reminder of the something that Israel refuses to acknowledge--that it is a nuclear power. One reason for this is that the U.S. sends billions in aid to Israel every year--but U.S. law forbids such aid to countries that are developing nuclear weapons and don't permit international inspections of their facilities. Thus, it is in the interests of both governments to maintain the fiction that Israel doesn't have a nuclear weapons program.<p>
Vanunu speculated to the <i>Guardian </i>that the charges might be an attempt to put him back in prison--or else a way of silencing him during an international campaign to have the gag order and other restrictions on him lifted when they come up for renewal later this year.<p>


