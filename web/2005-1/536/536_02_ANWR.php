<font face="Times New Roman, Times, serif" size="4">Congress approves drilling in Alaskan wildlife refuge</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A helping hand for oil bosses</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lance Newman</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 25, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE BUSH administration got another gift from its wish list when the Senate gave backdoor approval to oil drilling in the Alaska National Wildlife Refuge (ANWR).<p>
The White House and its oil industry supporters have been clamoring for access to the coastal plain of ANWR for years, but previous efforts to open up the nearly 9 million acres of pristine wilderness to oil exploration have stalled. This time, Republican senators snuck the proposal into a budget resolution, and the provision survived an attempt to remove it by a 51-49 vote.<p>
This is only the latest point in the Bush administration's stepped-up assault on the environment.<p>
On March 4, Bush nominated Stephen Johnson to head the Environmental Protection Agency (EPA). Johnson is a career bureaucrat known for his easygoing relations with big business during his years running the Office of Prevention, Pesticides and Toxic Substances. Before his nomination, he traveled the country stumping for Bush's misnamed Clear Skies initiative and speaking in favor of deep cuts to the EPA budget. <p>
Then, on March 15, the EPA released new regulations that claim to limit mercury pollution from coal-fired power plants--but, in fact, will concentrate this especially toxic pollution in poor towns and neighborhoods. <p>
Bush brags that the EPA's new rules will cut mercury emissions from 48 tons this year to 24 tons in 2020. But instead of making all power companies reduce their emissions, the rules set up a pollution-credit trading scheme, so that improvements at new plants can be used to let the filthiest polluters off the hook. Mercury is proven to cause chronic nerve damage when it accumulates in the body. Children are especially vulnerable to its effects, and it has been linked to autism, a debilitating disease that reached epidemic levels in the last decade. <p>
One day after the mercury regulations were released, the Senate voted on ANWR. ANWR was established in 1960, one year after Alaska became a state. But in 1969, major oil reserves on the North Slope were proven with the sinking of the first well at Prudhoe Bay. The resulting frenzy culminated in the completion of the Trans-Alaska Pipeline in 1977. <p>
In 1980, Congress doubled the size of ANWR--but slipped in a provision giving itself the power to allow drilling at some time in the future. Ever since, Big Oil has been clamoring for access to the refuge, which is home to some of the last healthy populations of caribou, musk oxen and polar bears, and hosts millions of migratory birds. Congress passed a bill authorizing oil drilling in 1995, but stiff opposition from grassroots environmental groups led Bill Clinton to veto the measure. <p>
The battle heated up again when Bush took office. During his first term, the House repeatedly passed bills authorizing ANWR drilling that were defeated in the Senate. But last week, Senate Republicans--joined by a few crucial Democrats--voted down an amendment that would have stripped anticipated revenues from oil leases in ANWR from the 2006 budget resolution. Though it may seem obscure, this vote clears the way to approving drilling later this year.<p>
The Bush administration claims that drilling in ANWR is critical to national energy security, and the oil corporations say the area could yield 1 million barrels per day. But the truth is that this would have no effect on dwindling supplies and skyrocketing oil prices. <p>
World oil consumption is nearly 80 million barrels a day, or 30 billion barrels per year. And at least a decade of exploration and development would be required before any crude flows out of ANWR. <p>
U.S. energy companies pumped more than $70 million into Republican campaign coffers in the last five years. Now, the Bush administration wants to pay them back--even if it means poisoning working-class children and destroying one of the last pristine habitats on the planet. <p>


