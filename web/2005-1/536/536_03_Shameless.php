<font face="Times New Roman, Times, serif" size="4">Politicians use Schiavo case to push Christian Right's agenda</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A shameless spectacle</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 25, 2005 | Page 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THE POLITICIANS in Washington stooped to a new low this week as George Bush and the Republicans vied with the Democratic "opposition" in grandstanding over the case of Terri Schiavo.<p>
Bush cut short his vacation to rush back to Washington and sign emergency legislation that could pave the way for doctors to reattach a feeding tube to the 41-year-old Schiavo, who has been in what doctors call a "persistent vegetative state" for the past 15 years following a heart attack.<p>
On March 18, a Florida judge decided in favor of Schiavo's husband Michael, who says that his wife had expressed to him her wishes not to be kept alive by artificial means. The Florida court made its ruling after hearing testimony from physicians who say that Schiavo's brain damage is so severe that there's no hope she will ever have any cognitive abilities. Schiavo's parents insist that their daughter should be kept alive.<p>
The right wing has exploited weaknesses in Michael Schiavo's case, such as a lack of proof of what Terri said about her wishes. But the right-wingers who shamelessly jumped on this case don't care about Terri or Michael Schiavo. They're advancing their own careers.<p>
In 2003, Florida Gov. Jeb Bush intervened to push through a state law that would reattach Schiavo's feeding tube. This time, brother George and his friends in Washington are grabbing the spotlight, passing an emergency law that paves the way for federal courts to intervene and overrule the Florida decision.<p>
"We should investigate every avenue before we take the life of a living human being," House Majority Leader Tom DeLay lectured. This from one of the most powerful politicians in Texas--the execution capital of the United States. DeLay and the Republicans say they care about the "sanctity of life." But apparently, the lives of the more than 100,000 victims of Washington's slaughter on Iraq aren't quite as "sacred" to them.<p>
The most vocal calls for intervention on behalf of Schiavo's parents have come from the Christian Right, the same people who oppose women's right to abortion and hate gays and lesbians. They are people like Randall Terry, founder of the fanatical anti-abortion group Operation Rescue, which harasses women trying to visit health clinics.<p>
In mobilizing around the Schiavo case, they have shown just how low they'll go. Indeed, one of the right-wingers who tried to invade the hospice to "deliver water" to Schiavo was the white supremacist Bo Gritz.<p>
"Today, it's Terri, tomorrow, it's another disabled person," said Tony Perkins, president of the right-wing Family Research Council, one of groups that pushed Congress to act. We've tolerated abortion in this country for the last 30 years, and now we're talking about eliminating those who cannot speak for themselves."<p>
If the Christian Right was so concerned about such questions, where were they on January 19, when Donald Beardslee, who suffered brain damage from a birth defect, was executed by the state of California? When was the last time that members of Congress flew back from their vacations because they realized that they had slashed the budget for services for the disabled?<p>
U.S. society is grossly negligent in its treatment of people with disabilities--from providing decent health care to making jobs accessible and available to them. A humane society would devote whatever resources are needed to help the disabled and their loved ones, who under this system bear most or all of the burden of care-giving.<p>
But this is not what the Schiavo case is about. As framed by the politicians, it is about promoting an agenda of bigotry and oppression--one that includes more restrictions on people's lives, such as limiting women's right to choose.<p>
Most congressional Democrats have jumped on board--giving up the mainstream debate to the distortions and myths of the right wing.<p>
Yet many people in the U.S. see through the Washington grandstanding. According to ABC News, an opinion poll taken this week found that respondents, by a margin of 63 to 28 percent, supports the removal of Schiavo's feeding tube. And an overwhelming majority, 87 percent, said that if they were in the same condition as Schiavo, they, too, would want their feeding tube removed.<p>
This shows the widespread support for the right to physician-assisted suicide. And it shows how desperately people would like moralizing politicians to keep out of their personal lives--and not make matters that much worse when difficult decisions must be made about loved ones who are being kept alive by machines.<p>
The politicians are exploiting a tragic situation for political gain. We should call them what they are--shameless hypocrites.<p>

