<font face="Times New Roman, Times, serif" size="5"><b>Saved from deportation</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">March 25, 2005 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">"I SAT down and looked around to see if anybody was as happy as I was, but no one paid attention because the whole school went crazy." Those were the words of Obain Attouoman, a teacher at Boston's Fenway High School, who had been faced with deportation under the Department of Homeland Security's "war on terror." A ruling in March which grants Attouoman a two-year reprieve.<p>
This victory is due entirely to the year-long struggle by Fenway High students, teachers, parents and community organizations. Attouoman's students--mostly minority and many of them immigrants--were central to the victory. <p>
When Attouoman missed a deportation hearing because he misread a date on a confusing handwritten letter, he was arrested in November 2003. Fenway students began to organize demonstrations immediately after his arrest. Protests of hundreds of high school students and staff and aggressive press conferences led to his release in March 2004--one day after the largest and most vocal rally.<p>
Attouoman was part of a political family in war-torn Ivory Coast, where he was a union organizer and political activist. He fled to the U.S. in 1992 after his second jail term in a year. In 1994, he applied for political asylum from the federal government--but the request has never been granted.<p>
Since his release from jail in March 2004, Attouoman has lived in limbo, forced to have his belongings packed for an impending deportation. But his students kept up the campaign. Three months ago, Attouoman was granted another waiting period that would have ended March 4--with a new deportation hearing and new goodbyes. <p>
The week of March 4 saw two demonstrations of 300 to 400 each. Students decided to take trains to Washington, D.C., to lobby senators. On March 4, Attouoman received a call from Sen. John Kerry's office notifying him of the two-year reprieve.<p>
A recent Boston Celtics game was dedicated to Obain Attouoman. When the stadiums' camera panned on him, the crowd gave him a standing ovation. Then the camera panned to Massachusetts Gov. Mitt Romney (probably to thank him for being involved in the campaign), and the crowd booed him.<p>
Attouoman's lawyer recently expressed the outrage of many who campaigned for him: "There's a problem in the system. It's almost embarrassing to live in a democracy where this could happen."<p>
The reality is that it wasn't the "system" that worked for the Fenway High teacher. Despite Sen. Kerry, Rep. Michael Capuano (D-Mass.), Boston Mayor Thomas Menino and even Gov. Romney advocating for him, judges continued to rule against Attouoman. <p>
Kerry has claimed that Attouoman's victory came because it was a bipartisan project. But while Attouoman's reprieve is a great step forward, it is far from the reality for most immigrants threatened under the USA PATRIOT Act--another "bipartisan project." Thousands of Arabs and Muslims have been imprisoned without charges and without access to lawyers, and more and more people like Attouoman face attacks through the escalating assault on civil liberties. <p>
Fenway High School students and teachers have shown the way by not allowing Obain Attouoman's story to be buried. They show that we can only confront the attacks on civil liberties with a confident and aggressive fight that doesn't take "no" for an answer.<br>
<b>Alpana Mehta, </b>Boston<p>

