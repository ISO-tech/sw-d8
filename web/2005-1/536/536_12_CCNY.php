<font face="Times New Roman, Times, serif" size="4">The battle at CCNY</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Defending our right to protest</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Monique Dols</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 25, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">MORE THAN 150 students, professors and activists converged on City College of New York (CCNY) in Harlem March 17 to defend our right to protest.<p>
The demonstration was called in response to the arrest of three CCNY students and one staff member who peacefully protested the presence of military recruiters at a campus job fair. Campus security maneuvered the protesters out of the job fair and into an empty hallway, where they attacked the protesters. Yet the four are the ones facing assault charges.<p>
Students Nick Bergreen, Hadas Thier and Justino Rodriguez have since been suspended from school and banned from setting foot on campus. Carol Lang, a secretary at the university, was suspended for 30 days without pay.<p>
Last week, the case was featured on the left-wing radio program <i>Democracy Now!</i> where Thier and Lang were interviewed, along with former Marine recruiter and antiwar activist Chris Dugan. "We have witnesses on the scene who know that we were just peacefully protesting, and it was actually security that was violent with us," Thier said. <p>
At CCNY, a campaign to defend the four has been launched, drawing more than 20 activists to a first meeting with less than 24-hour notice. The campaign started circulating a letter of support for the four, which has gathered over 1,000 signatures in just three days. The outpouring of solidarity has come from a broad range of groups, including the Professional Staff Congress (PSC), New York Civil Liberties Union, Campus Antiwar Network, War Resisters League, United for Peace and Justice, Out Now Coalition, International Socialist Organization and many more. <p>
At the March 17 protest, Bill Crane, a professor of psychology at CCNY, told the crowd, "This is a part of the repressive condition of the nation. When they can initiate pre-emptive strikes against any country, it means aggression is allowed. This attack is a pre-emptive strike on students for their political beliefs." <p>
The day before the protest, left-wing author and activist Mike Davis was invited by the administration to speak at CCNY about his book <i>Planet of Slums. </i>To the dismay of his hosts, Davis spent several minutes at the beginning of his presentation condemning CCNY's attacks on the four.<p>
"City College has a glorious history of student and anti-militarism protests," said Jim Perlstein, a retired professor who taught in the public schools for 37 years. "It also has an inglorious history of trying to suppress those protests." <p>
But the outpouring of support for the four suggests that the administration's crackdown may be backfiring. "In an attempt to stifle the progress of counter-recruitment, the CCNY administration and the campus cops are throwing everything they have at us," Justino Rodriguez said. "They are using baseless claims and lies to intimidate us, turn our lives upside down and to set back the movement. Little do they know they are having the opposite effect."<p>
In fact, the attack at CCNY is has brought together many people and strengthened their commitment to building a stronger movement. In just the week since the arrests at CCNY, students at Hunter College staged a protest of military recruiters at their career day, and other activists at the Borough of Manhattan Community College (BMCC) successfully kicked the Army off campus.<p>
"I think the effect of counter-recruitment work has been great," Thier told <i>Democracy Now! </i>"And I think that that's why they were prepared for us, and why they cracked down on us in the way that they have. <p>
Counter-recruitment protests are picking up momentum across the country--and tapping into the anger and discontent with the Bush administration's occupation of Iraq. We can't let them get away with trying to silence this struggle.<p>
