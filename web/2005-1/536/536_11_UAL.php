<font face="Times New Roman, Times, serif" size="5"><b>Feds take over a United pension plan</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 25, 2005 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">PENSION PLANS for 36,000 active and retired ground employees at United Airlines were seized by a federal government agency this month as the bankrupt company continues its drive to wipe out decades of gains in wages and benefits.<p>
United extracted $2 billion in concessions from its unions through negotiated concessions and cuts imposed by a federal bankruptcy judge after filing for bankruptcy in 2002.<p>
In the summer of 2004, in the midst of negotiating for further cuts, United management illegally suspended payments on insurance premiums to the government's Pension Benefits Guarantee Corp. (PBGC), the agency that insures retirement plans for many large corporations. The failure to pay these $363 million in premiums has aggravated United's pension shortfall. The company claims it has assets of just $1.2 billion to cover potential liabilities of $4.1 billion. If the courts approve the PBGC's takeover of the fund, benefits to retirees would be reduced to 20-50 percent of what workers were originally promised. <p>
United has used the pension issue to play the Air Line Pilots Association (ALPA) against other unions. Management made a separate deal to convert pensions of ALPA members to a 401(k) cash benefit retirement plan--which could save United billions--contingent on other unions making a similar deal.<p>
The pilots' union leaders calculated that, even with pay cuts, they could afford to fund such a plan--even though lower-paid workers could not. A federal bankruptcy court judge, however, ruled the deal illegal. The PBGC, meanwhile, moved to seize control of the pilots' plan, a move that has been challenged in court by ALPA.<p>
By ratcheting up the pressure on pensions, United succeeded in obtaining a new round of concessions from the Association of Flight Attendants (AFA), now affiliated with the Communications Workers of America. In a deal ratified in February, AFA members took a 9.5 percent pay cut as well as reduced vacation pay and cuts in bonuses. The International Association of Machinists (IAM), which represents ramp workers, agreed to a short-term 11.5 percent wage cut under pressure from a bankruptcy judge in a deal that expires in April.<p>
Ironically, the IAM, rejected by United's 7,000 mechanics for its failure to stand up to management, has threatened to strike over the pension takeover, while the Airline Mechanics Fraternal Association (AMFA) appears resigned to the move. The coming weeks could be decisive for the future of unions at United.<p>


