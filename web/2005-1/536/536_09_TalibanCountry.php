<font face="Times New Roman, Times, serif" size="5"><b>The other U.S. occupation</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Chuck Stemke</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 25, 2005 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Taliban Country, </b></i>a documentary by Carmela Baranowska. Visit <a href="http://www.talibancountry.com" target="_blank">www.talibancountry.com</a>.</font><p>

<font face="Times New Roman, Times, serif" size="3">IN THE beginning of the film <i>Taliban Country, </i>Australian filmmaker Carmela Baranowska is traveling as an embedded reporter with a U.S. Marine company into a remote and incredibly poor area of Afghanistan. She is frustrated as it becomes clear that they're taking her on selected missions where the Marines will come across favorably.  <p>
Some things she sees just don't add up, like some arbitrary arrests, the heavy-handedness of the Marines and the strange relationship they have with the local, Hamid Karzai-appointed governor of the region and his militia.<p>
They also have a strange idea about how to "win hearts and minds." After kicking in all of the doors in a village, rounding everyone up and rummaging through their belongings, a "good cop" Marine officer comes in and offers to drill some wells, as if that would make it all better.<p>
So Baranowska breaks out on her own and returns to the villages with a translator. Without the Marines around, she gets a very different story. The local people are fed up with the casual brutality of the U.S. At one point, several men recall how 35 of them were taken away in helicopters and forced into a humiliating strip and body-cavity search as the soldiers laughed at them.<p>
They also point out how the governor, the official local representative of the government, is actually a warlord who is using his new position to advance the power of his own militia. With the help of the Marines, he's disarming all of the other surrounding communities, which he rules purely through fear.<p>
Sadly, most Americans--even many who are opposed to the war in Iraq--accept that the occupation of Afghanistan is a just cause. They ought to see this movie.<p>

