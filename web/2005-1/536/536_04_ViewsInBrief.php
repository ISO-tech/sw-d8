<p><a name="Occupation"></a><font face="Times New Roman, Times, serif" size="4"><b>A permanent occupation</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AMERICANS ARE being snookered by Republican and Democratic claims that the U.S. will withdraw its troops "when the Iraqis are able to guarantee their own safety." <p>
Our government has no intention of leaving Iraq to the Iraqis. Evidence? In addition to a 3,000-person embassy in the Green Zone (our largest and most CIA-infested in the world), the U.S. is busy building 13 permanent airbases throughout the country. <p>
According to the <i>NewYorker</i> journalist Seymour Hersh and numbers of returning veterans, these include elaborate quarters for Air Force personnel and at least one million-dollar-plus gymnasium complex. That's where the bulk of our $20 billion reconstruction tax money is going--not into clean water and electricity for desperate Iraqi citizens.<p>
The corporate-controlled media refuse to report these incriminating facts. They refuse to confront Bush and the Pentagon with the evidence of permanent occupation.<br>
<b>Abbott Ikeler,</b> Cumberland, R.I.<p>

