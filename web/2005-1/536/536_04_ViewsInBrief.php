<font face="Times New Roman, Times, serif" size="4"><b>Rank-and-file teachers fight</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THANKS TO Randy Childs for his article ("Reformers win UTLA vote," March 11). I've been reading about the recent election, but his is the first rank-and-file perspective I've found.<p>
I wanted to mention one thing--you might want to explain your use of the words "reformers" and "progressive" (as in Progressive Educators for Action). I say this because the "other side," if you will, uses those same words to describe themselves. (Just like the right wing to try to confuse people.) <p>
I'm thinking specifically of Adam Urbanski and the Teacher Union Reform Network. In fact, Childs' local, presumably under the leadership of John Perez or perhaps his predecessor, joined this group. They are a group of American Federation of Teachers and National Education Association locals that push for a type of "reform" that amounts to class-collaborationist policies and pushing pay-for-performance schemes on their members.<p>
Congratulations on the recent elections. I am very excited to see that one of the largest teacher union locals in the country is becoming more militant and rank-and-file oriented.<br>
<b>Debra Brim,</b> from the Internet<p>

