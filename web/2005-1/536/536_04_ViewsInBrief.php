<p><a name="Bush"></a><font face="Times New Roman, Times, serif" size="4"><b>Rolling over for Bush</b></font><p>

I WAS pleased to see Community College of Rhode Island President Thomas Sepe denouncing Bush's attacks on education ("Bush's Class War Budget," March 11). Unfortunately, I had to read about it in <i>Socialist Worker</i>. The <i>Providence Journal</i> was too busy quoting Sen. Jack Reed (D-R.I.). <p>
Reed took center stage at the press conference, saying that he was going to fight against cuts to the Upward Bound Program.<p>
However, Reed happens to be a member of the Senate Armed Services Committee. Just nine days before his appearance at the press conference, he announced the introduction of a bill, co-sponsored with Chuck Hagel (R-Neb.), to expand the size of the Army by 30,000 troops, and the Marines by 3,000. Cost: $2.7 billion, or almost nine times the amount needed to fund Upward Bound.<p>
This man is going to fight Bush's cuts? <p>
The same day, Rhode Island Congressional Democrats Patrick "I-haven't-worked-a-day-in-my-life" Kennedy and James "Right-to-lifers-love-me" Langevin spoke about fighting the cuts at a press conference on aid to the homeless. Meanwhile, 8,000 Rhode Islanders are on a waiting list for Section 8 housing aid.  <p>
Rhode Island is a solidly "blue state," and Reed and Kennedy have been in Washington longer than Bush. If they're really so upset about Bush's budget, why haven't they solved this crisis already? These men are liars and hypocrites!<br>
<b>Brian Chidester,</b> Warren, R.I.<p>

