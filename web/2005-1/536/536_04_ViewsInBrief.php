<p><a name="Fraud"></a><font face="Times New Roman, Times, serif" size="4"><b>The tough-on-crime fraud</b></font><p>

<font face="Times New Roman, Times, serif" size="3">HOPING TO bring capital punishment to Massachusetts, Gov. Mitt Romney is preparing to propose a bill that would legalize state executions. This comes against the backdrop of Connecticut preparing to be the first state to execute a prisoner in New England in 45 years. <p>
Two new jails, costing the state some $59 million--one of which will house federal immigration detainees--are being planned or are under construction in western Massachusetts. In the past two years, the state has lost over 34,000 jobs, and many of our communities have faced budget crises.<p>
Why is the state looking to build new jails--with more beds--while cities and towns alike have to make drastic cuts? Romney may be trying to score political points with his tough-on-crime policies, but building more jails or resurrecting the death penalty does nothing for our communities struggling with the real issues of unemployment, affordable housing and cuts in public education. <p>
In response to these issues, the Northampton ISO will be organizing a public forum, including such longtime anti-death penalty activists as Shujaa Graham, Robert Meeropol, Russell Neufeld and local community organizer Holly Richardson. The event is sponsored by American Friends Service Committee, Arise for Social Justice, the Campaign to End the Death Penalty, the International Socialist Organization, Out Now, Traprock Peace Center and Western Massachusettes Citizens Against the Death Penalty.<br>
<b>Anthony LeBeau,</b> Northampton, Mass.<p>

