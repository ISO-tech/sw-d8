<font face="Times New Roman, Times, serif" size="5"><b>The genocide that the West ignored</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Phil Gasper</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | Janury 21, 2005 | Page 13</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Hotel Rwanda, </b></i>directed by Terry George, starring Don Cheadle, Sophie Okonedo and Nick Nolte.</font><p>

<font face="Times New Roman, Times, serif" size="3">DURING THE course of 100 days in 1994, members of Rwanda's majority Hutu group carried out a mass slaughter of the country's minority Tutsis and moderate Hutus, leaving upwards of 800,000 people dead. The massacre was spearheaded by an informal Hutu militia, called the Interhamwe, in collusion with the Hutu government and army, and spurred on by viciously racist radio broadcasts that labeled Tutsis as "cockroaches."<p>
Planned months in advance, the slaughter began after the Hutu president's plane was shot down as he was returning from signing a peace agreement with Tutsi rebels. The government blamed Tutsis, although the perpetrators were almost certainly Hutu hardliners in the military.<p>
Although there was a United Nations peacekeeping force in the country, and although the UN and western governments received warnings of the impending massacre, nothing was done to stop the killing. Foreign nationals were evacuated, but no effort was made to save Rwandans whose lives were in danger.<p>
In fact, Bill Clinton's State Department instructed its officials not to refer to the Rwanda killings as "genocide," since this would have triggered international treaty obligations to offer assistance. The massacres only stopped when the Tutsi-led rebel army overthrew the Hutu government, triggering a mass exodus of Hutus into the Congo.<p>
The enormous horror of the Rwanda tragedy and the staggering racist indifference of the West, which saw no vital strategic interests at stake, is brilliantly captured in<i> Hotel Rwanda. </i>The film is directed by Terry George, previously best known for films on the Irish struggle such as<i> In the Name of the Father</i> and<i> Some Mother's Son</i>.<p>
<i>Hotel Rwanda </i>tells the true story of Paul Rusesabagina, played by Don Cheadle in the most powerful performance of his career, who is the Hutu house manager of Sabina's four-star Milles Collines Hotel in Rwanda's capital Kigali. Although he has little interest in politics, his job requires him to develop contacts with foreign diplomats and members of the Rwandan elite.<p>
Rusesabagina's wife, Tatania (played by Sophie Okonedo in another exceptional performance) is Tutsi, and this, together with his own basic decency, leads him to play a heroic role.<p>
At first, Rusesabagina is confident that the international community will stop the extremists, but it soon becomes terrifyingly clear that this will not happen. Nick Nolte plays the Canadian UN commander who is desperate to help but is prevented from doing so by his superiors.<p>
Rusesabagina is forced to open his hotel to hundreds of Tutsi refugees. By taking advantage of his high-placed contacts, luck and incredible ingenuity, he manages to protect more than 1,200 Tutsis for weeks.<p>
<i>Hotel Rwanda</i> does an amazing job of portraying the Rwanda genocide through the eyes of some of the individuals caught up in it. Perhaps unsurprisingly, it is less effective in casting light on what led to such brutality in the first place.<p>
The movie does draw attention to the fact that the Hutu-Tutsi division was a creation of the Rwanda's racist former Belgian colonial regime, but there is no mention of the economic catastrophe, precipitated by International Monetary Fund and World Bank austerity programs, that preceded the slaughter.<p>
There is also a danger that the film will be used in an attempt to justify future Western military interventions that claim to be humanitarian. But the truth is that indifference and military invasion by imperialist powers are simply two sides of the same coin--different ways of advancing their own strategic goals.<p>
What is desperately needed is genuine development aid and a complete transformation of international power relations. These are not issues that<i> Hotel Rwanda</i> addresses. What it does do superbly is to show the human side of the tragedy and portray the courageous response of one man to an unimaginable catastrophe. Everyone should see it.<p>

