<font face="Times New Roman, Times, serif" size="4">A U.S. soldier writes to George Bush:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"The soldiers who died deserved better"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">January 21, 2005 | Page 6</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
When the history of the movement that finally stopped the war in Iraq is written, the letter below will have a very special place. Coming from a serving soldier, and a sergeant, who feels a special responsibility to the troops that serve under him, it is a declaration of principle and allegiance to the liberties of the people of the United States, in the spirit of Tom Paine and Patrick Henry.<p>
Read it with loving care, consider carefully what it means, carry it with you and draw strength from it. And if you come across one or some of our troops, have spare copies to pass along.<br>
<b>Thomas Barton, </b>New York City<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3">November 20, 2004<br>
To: George W. Bush<p>

When are you going to tell the truth to the people of the United States? Why don't you tell them why you want to be in Iraq so bad?<p>
I was there for six months, and I did not see the first weapon of mass destruction. I did receive orders from the company commander to shoot children if they threw small rocks at us, and that was when I figured out that the entire thing was way over the line.<p>
Over 1,200 soldiers have died in Iraq so that you can have a couple billion more dollars--that should make you feel very good about yourself. The soldiers that have died for this sham that you have put over on the American people are so much more deserving than that. <p>
If you truly had respect for the military and the people that serve, then you would not continue to kill them in your war.<p>
I joined the Army to protect my country and not to be a mercenary for a political despot. If you wish to put me in prison because of my views, then you should make room for about 75 percent of the military. <p>
And while you are at it, make some room for yourself and about half of your administration. You are responsible for what happened at Abu Ghraib, and you are shirking your responsibility. The commander in chief is not above the Uniform Code of Military Justice, as you would like to believe.<p>
I want to fulfill my contract that says I joined the Army to protect my country against all enemies foreign and domestic, and as far as I am concerned, you are a domestic enemy of the United States. You care nothing for this country; you just care about the profits that are to be made from the oil in Iraq. That much is evident to me from the way the contracts were passed out to Halliburton and Kellogg, Brown and Root. <p>
It must be nice to have the deck stacked in your favor by the president of the USA.<p>
Since you are raising the debt ceiling of America so that we can pay the bills that you have run up, why don't you forgive the debts of every one in the armed forces--since they are the ones who are making it possible for you to make billions from the oil from Iraq.<br>
Sincerely, <b>Sgt. Kevin M. Benderman</b> <p>

