<font face="Times New Roman, Times, serif" size="4">Taxing the rich could pay for California's budget shortfall</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Schwarzenegger's brutal budget attack</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | Janury 21, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">IN HIS January 5 State of the State address, California Gov. Arnold Schwarzenegger unveiled a budget that proposes billions in cuts for key social programs--from health care to education to welfare.<p>
Schwarzenegger justified his proposal by claiming that he was taking on "special interests" in Sacramento. "Any time you try to remove one dollar from the budget, there are five special interests tugging on the other end," he said. But to judge from Schwarzenegger's budget, the "special interests" he's referring to must be children, the poor, persons with disabilities, schoolteachers and retired workers.<p>
Here are some of the cuts Schwarzenegger proposed:<p>
-- Freezing cost-of-living increases for the elderly and disabled recipients of Supplemental Security Income and poor families covered by California's welfare-to-work program.<p>
-- Eliminating cost-of-living increases and requiring premium payments for low-income recipients of Medi-Cal health insurance.<p>
-- Privatizing the pension system for public-sector workers and replacing it with 401(k)-style retirement accounts.<p>
-- Slashing $2.2 billion from public education.<p>
These cuts were announced just two days after a Rand Corp. study revealed that California lags behind the national average in school spending and teacher pay, and has the second-highest student-to-teacher ratio in the country.<p>
The education cuts in particular are the bitter fruit of a failed strategy of appeasement pursued by California's teachers unions and other education groups--who agreed to support $2 billion in education cuts last year in exchange for Schwarzenegger's promise not to do it again.<p>
Even a tiny increase in the taxes paid by rich Californians could easily pay for all of these budget shortfalls. But Schwarzenegger would never propose this. He's dedicated to the agenda of California's real special interests--wealthy individuals and corporations. <p>
If unions and other progressive groups launched a grassroots movement against the Governator's agenda instead of making backroom deals, we could stop these cuts.<p>

