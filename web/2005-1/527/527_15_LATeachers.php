<font face="Times New Roman, Times, serif" size="4">Opposition mounts challenge in LA teachers union</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>High stakes in UTLA vote</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs, United Teachers Los Angeles</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | Janury 21, 2005 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">MEMBERS OF United Teachers Los Angeles (UTLA), the second-largest local teachers' union in the country, will vote in February for the leadership of our union. This election breaks down into two choices for UTLA members: the Leadership slate led by incumbent President John Perez, or the rank-and-file opposition slate United Action.<p>
UTLA members have been without a new contract since July 2003 and haven't had a raise since a meager 3-percent boost in 2002. Class sizes continue to climb in the Los Angeles Unified School District (LAUSD)--home to some of the most overcrowded schools in the U.S.<p>
LAUSD's central bureaucracy has tripled in size since the late 1990s, and these administrators make their presence felt with a torrent of one-size-fits-all mandated programs and tests.<p>
Dozens of LAUSD schools face punitive actions under George Bush's misnamed No Child Left Behind law. California schools face their fourth consecutive year of budget shortfalls, and Gov. Arnold Schwarzenegger has declared war on teachers' seniority rights with his "merit pay" proposal.<p>
In the context of these attacks on teachers, the strategies offered by Leadership and United Action are like night and day. Last year, when LAUSD threatened massive cuts in health care benefits, John Perez circulated a survey asking what kinds of cuts we would prefer. United Action candidates took the initiative in organizing a rally of hundreds of teachers outside LAUSD headquarters that demanded--successfully--that the district fully fund our benefits.<p>
The Leadership slate makes no mention of class sizes in its campaign literature. United Action candidates have made class-size reduction a central demand in their platform and call for a mass mobilization campaign to pressure the district to cut class sizes.<p>
In short, John Perez and the Leadership slate offer UTLA members nothing but the same business unionism that has failed to defend us from the district's attacks. It's time for a change. Every UTLA member who wants a union that will fight for teachers and students should vote United Action.<p>
These elections won't fix UTLA overnight. Activists in Progressive Educators for Action (PEAC), who make up most of the United Action slate, will need to continue building their rank-and-file reform network after the elections, regardless of the results. But an election victory for United Action will be a huge step forward for LA's teachers.<p>
