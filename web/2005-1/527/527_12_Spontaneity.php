<font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Getting from anger to action</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | Janury 21, 2005 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">THOUSANDS OF elderly Russians took to the streets on January 15 to protest a new law "abolishing a wide range of social benefits for the country's 32 million pensioners, veterans and people with disabilities," according to the <i>New York Times.</i><p>
One protester called it a spontaneous outpouring, a "tsunami," involving many people who had never protested before. "Tsunami" conveys well the idea that the struggle appears to well up from nowhere, but which in reality has its origins in deep but unseen rumblings under the surface.<p>
The truth is, there is no such thing as "pure" spontaneity. As the Italian revolutionary Antonio Gramsci wrote in the1920s, "every 'spontaneous' movement contains rudimentary elements of conscious leadership" and "of discipline."<p>
Gramsci defines spontaneity as a struggle or movement that is "not the result of any systematic educational activity on the part of an already conscious leading group, but have been formed through everyday experience illuminated by 'common sense,' i.e. by the traditional popular conception of the world."<p>
All outbreaks of struggle have some degree of this kind of "spontaneity," in the sense that they cannot be simply called on the basis of the will of groups or organizations, but must reflect or connect with real sentiment that is strong enough to evoke a response.<p>
Spontaneous, therefore, is a matter of degrees. On the extreme end of spontaneous are struggles where there is a high degree of improvisation by people without prior experience of struggle and organization.<p>
As the late British socialist Duncan Hallas wrote, "Spontaneity is a fact. But what does it mean? Simply that groups of workers who are not active with any political or even trade union organization take action on their own behalf or in support of others. From the point of view of organizations, the action is 'spontaneous'; from the point of view of the workers concerned, it is conscious and deliberate."<p>
That will certainly characterize, out of necessity, many of the future struggles in the United States. Many of what appear to be outbursts of spontaneous struggle have had the ground prepared for them by years of patient organizing among smaller numbers, unseen but nevertheless there.<p>
The groundwork for the mass unionizing drives and sit-down struggles of the 1930s was laid, in part, by the tireless work of individual Communist and Trotskyist militants organizing among workers years before. Smaller numbers of activists, likewise, engaged in years of less successful organizing that laid the foundation for the civil rights movement.<p>
If there's one thing needed in the U.S. today, it is a Russian-style outpouring against Bush's attempts to wreck Social Security.<p>
Even one mass demonstration against the dismantling of Social Security would have an incalculable moral effect on the working class in terms of the revival of a fighting mood in this country. But the mainstream organizations seem unwilling or incapable of making even the semblance of a fight.<p>
The conditions of anger exist around a number of issues--from the war in Iraq to bans on gay marriage to declining living standards for workers, notwithstanding the arguments being peddled among liberals that now is the time to hunker down and be "realistic" (i.e., adapt to the right). But the confidence, experience and organization are lacking.<p>
The question for socialists today is how to begin bridging the gap between muted anger and collective action--even if it may at first be only in smaller struggles&#8218;in the context of nonexistent (or completely anemic, noncombative) organization.<p>
To quote Hallas again: "Spontaneity and organization are not alternatives; they are different aspects of the process by which increasing numbers of workers can become conscious of the reality of their situation and of their power to change it. The growth of that process depends on a dialogue, on organized militants who listen as well as argue...and who are able to find connections between the actual consciousness of their fellows and the politics necessary to realize the aspirations buried in that consciousness."<p>

