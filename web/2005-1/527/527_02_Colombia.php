<font face="Times New Roman, Times, serif" size="4">New step in Colombia's war</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Rebel leader to stand trial in U.S.</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Stuart Easterling</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | Janury 21, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">BY EXTRADITING a leader of the country's left-wing rebels to the U.S., Colombia's right-wing government has escalated a four-decades-old war--and increased the direct involvement of its Washington backers.<p>
Colombian authorities last year arrested Ovidio Ricardo Palmera, also known as Sim&oacute;n Trinidad--the most senior guerrilla commander ever captured. Only a few years ago, Trinidad was the rebels' principal spokesperson during peace talks with the government. <p>
Now, he has been extradited to the U.S. as a common criminal. This is a first in the history of Colombia's civil war.<p>
President &Aacute;lvaro Uribe came to power in 2002 and immediately began a military escalation of the conflict with the guerrillas. This strategy was largely funded by the billions of dollars provided in military aid by the Clinton administration's "Plan Colombia" policy.<p>
In the past, the government and the Revolutionary Armed Forces of Colombia (FARC) negotiated prisoner exchanges. Uribe's move means that Trinidad--and any future extradited FARC leaders--can't be part of these since they will almost certainly be spending many years rotting in U.S. jails.<p>
Uribe is also showing that he would prefer to have the U.S. government do the work, rather than trying FARC rebels in Colombia. In other words, Colombia is sending its citizens to be tried by the same country that produced Abu Ghraib and Camp X-Ray in Guant&aacute;namo Bay, Cuba.<p>
Meanwhile, U.S. officials are delighted to be getting their hands on a FARC commander. He will likely be paraded around as another victory in the so-called "war on terror."<p>
Human rights organizations see the extradition as undermining future prisoner exchanges. Even the country's Catholic Church had asked that Uribe not move forward with the extradition while Trinidad faced charges of "rebellion" in his own country.<p>
Trinidad will apparently be charged with drug trafficking in the U.S.--due to the FARC's admitted policy of not stopping impoverished peasants from growing coca, and their taxation of the coca trade. But although more Colombians die from U.S. cigarettes than U.S. citizens die from Colombian cocaine, don't expect the tobacco company CEOs to be extradited to Colombia any time soon.<p>

