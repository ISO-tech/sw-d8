<font face="Times New Roman, Times, serif" size="4"><b>Iraq Veterans Against the War</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Keith Rosenthal</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BOSTON--Several veterans of the current war in Iraq will be going on an 18-stop speaking tour here from January 29 to February 6.<p>
Michael Hoffman, who was a corporal in the Marine Corps, and Kelly Dougherty, of the 220th Military Police Company, will be speaking at high schools, colleges and community forums, telling of the horrors they witnessed in Iraq and hoping to inspire people to get active in opposing the war and bringing all of their fellow soldiers home now.<p>
The speaking tour has been organized by the Iraq War Veterans Tour coalition, which is comprised of groups such as Military Families Speak Out, Iraq Veterans Against the War (co-founded by Hoffman and Dougherty), United for Justice with Peace, the International Socialist Organization, and many other campus, youth, community and labor organizations.<p>
There's been a lot of excitement surrounding the organizing of this tour. However, some don't want the tour to seem "too antiwar"--and successfully argued against having one of the speaking tour stops be at an antiwar event with former presidential candidate Ralph Nader and renowned author and activist Howard Zinn. So instead, the tour's first stop will be an event in which an array of Democratic politicians are being invited to hear the Iraq veterans' testimonials. <p>
This approach fails to understand that we cannot expect change to come through the politicians of the second-most pro-war party in Washington. Nevertheless, the tour can help form and grow antiwar groups and activism, which can help people can get involved in the antiwar movement for the long haul.  <p>

<font face="Times New Roman, Times, serif" size="4"><b>Chicago counter-inaugural protest</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Glenn Allen</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--Some 300 people marched and rallied here January 15 to protest Bush's upcoming inauguration.<p>
The rally began at Senn High School, which was recently targeted by the Chicago Public Schools to become a Naval academy. The military school would occupy an entire wing of Senn's building, taking half of the school's science labs and a third of its classrooms. Students, teachers and community members have been organizing against this takeover. At Saturday's rally, a Senn student spoke out against the war and militarization of our schools.<p>
Protesters then marched to the offices of Alderwoman Mary Ann Smith and Rep. Janice Schakowsky (D-Ill.), both who support the Naval academy at Senn. Teachers' union representative Jesse Sharkey spoke against the naval academy and the Democratic politicians who are pushing it through.<p>
The march was spirited, despite frigid temperatures. Speakers at the final rally included Palestinian activists, gay rights activists and the father of a soldier killed in Iraq.<p>

