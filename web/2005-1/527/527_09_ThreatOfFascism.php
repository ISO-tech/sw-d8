<font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Are we facing the threat of fascism?</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | Janury 21, 2005 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">FACED WITH the prospect of four more years of George W. Bush, some on the left claimed that fascism was on horizon if Bush was re-elected.<p>
"We should not gloss over the reality that the Bush team has neared some elements of fascism in its day-to-day operations--and forces inside the Bush administration would be well-positioned to move it even farther to the right after 2004," left-wing columnist Norman Solomon warned. "We don't want to find out how fascistic a second term of George W. Bush's presidency could become."<p>
Solomon's argument was mainly an effort convince progressives considering a vote for Ralph Nader that they had no choice but to back Democrat John Kerry. After all, the logic went, if Kerry wasn't something that you wanted, at least he wasn't Bush.<p>
But was Solomon right to warn that Bush is, essentially, ushering in a fascist dictatorship?<p>
Certainly, during his first term, George W. Bush got away with more reactionary attacks than anyone had expected. The Bush regime steamrolled democratic rights (in stealing the 2000 vote) and elementary civil liberties (with the USA PATRIOT Act), scapegoated the oppressed (proposing a constitutional ban on gay marriage), and set his sights on world domination (with wars in Afghanistan and Iraq) and the subjugation and dehumanization of the world's people (torturing detainees at Abu Ghraib and Guant&aacute;namo).<p>
But this isn't the proof of fascism--because all of these assaults have been carried out in one form or another by governments in the U.S. and elsewhere that are considered democratic, and often enough when the ruling party was the "liberal" alternative.<p>
For many progressives, using the term fascism to describe life under the Bush administration is an attempt to shock people out of what is seen as "complacency" about Bush's rule. The assumption is that because Bush won the election, masses of the U.S. population have either been duped into supporting the Bush agenda, or didn't take the crimes of the Bush administration seriously enough to vote him out of office.<p>
But it's not splitting hairs to argue that there's no merit to using the word fascism where it doesn't apply.<p>
For one thing, this undercuts what fascism really is. Historically, fascism is a far-right movement of the middle classes (small business people, professionals, etc.), which have been ruined by severe economic crisis. As an ideology, fascism unites these forces with an appeal to a national or racial "identity." The aim of a fascist government is to crush workers' movements using street violence and to do away with even limited forms of bourgeois democracy.<p>
As appalling as the Bush administration's attacks have been, the situation today doesn't approach the violence of a fascist regime.<p>
What's more, if you define fascism to mean increased police powers and gutted civil liberties, then you have to consider the Democrats to be fascists, too. After all, Democrats and Republicans alike supported the USA PATRIOT Act--John Kerry and Edwards included. <p>
The FBI's vicious COINTELPRO program targeting left-wing organizations was carried out under Democrats John F. Kennedy and Lyndon Johnson, as well as Republican Richard Nixon. And Democrat Woodrow Wilson is responsible for the 1919 Palmer Raids that scapegoated socialists and immigrants.<p>
The other problem with labeling Bush and the Republicans as fascists is that it overstates the depth of their support.<p>
U.S. society is very polarized. Bush and the Christian Right have a hard core of supporters, but there is also very broad opposition. This is reflected in opinion polls that show less than half of the population approves of the Bush administration (48 percent, according to a survey published by ABC News and the <i>Washington Post </i>in December) and over half thought Bush's war in Iraq wasn't worth fighting (56 percent).<p>
What was missing during the 2004 election--and is still missing from the mainstream political debate--in a way for this anti-Bush sentiment to be expressed.<p>
We shouldn't underestimate the impact of what the Bush administration has been able to get away with. And Bush's Inauguration Day celebration will surely be an imposing display of police power--whether it's reported in the media or not.<p>
But this isn't fascism--and progressives who claim that it is are disorienting and disarming those who want to build a movement against it.<p>
This was certainly true during the election--when the claim that Bush was a fascist threat was used to drum up votes for John Kerry, who stood for most of the same right-wing policies as the Republicans and fumbled away every opportunity to effectively challenge Bush. <p>
The same point applies after the election. We need a clear idea of what and who we are fighting--and what it will take to build the opposition. <p>

