<font face="Times New Roman, Times, serif" size="5"><b>Arson clinic at Washington abortion clinic</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | Janury 21, 2005 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">"A RANDOM act." That's what federal officials called the January 9 fire at Eastside Women's Health Clinic in Olympia, Wash. Bureau of Alcohol, Tobacco, Firearms and Explosives (ATF) officials concluded that the fire was intentionally set, but didn't constitute an act of domestic terrorism.<p>
The federally funded health care center has been the target of anti-abortion picketers every Thursday--the day of the week it performs abortions--for the last 20 years. The fire also comes just weeks before January 22--the anniversary of <i>Roe v. Wade, </i>the 1973 Supreme Court decision that made abortion legal in the U.S.<p>
Yet Scott Thomasson of the ATF's Seattle office told the Associated Press, "It seems to be a random act at this time," and the FBI has withdrawn its investigators. While no one was injured, the fire caused damage estimated at $500,000.<p>
ATF agents say that they could not link the attack to any anti-abortion groups. But this isn't the first time clinic workers have been targeted. Allison Wolfe, the daughter of former clinic owner Pat Shively, told <i>The Olympian </i>newspaper about an attempt on her mother's life. "[My mother] wore a bulletproof vest to work," she said.<p>
And acts of terrorism are hardly new for the anti-abortion movement. According to the National Abortion Federation, since 1990, 146 acts of arson have been committed against abortion clinics. In 1993, Florida doctor David Gunn became the first abortion provider to be murdered by anti-abortionists. Since then, six more doctors have been killed, and several more have had attempts made on their lives.<p>
Despite the attack on her clinic, Eastside Women's Health Clinic co-owner Shelley Pacheco told <i>The Olympian, </i>"I'm not going to be kicked down that easily."<p>
Abortion rights activists must stand up and expose the anti-abortion terrorists for what they are--and push them back into the shadows where they belong.<p>

