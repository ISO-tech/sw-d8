<font face="Times New Roman, Times, serif" size="4"><b>Is Mississippi anti-<i>America</i>?</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CENSORSHIP DOESN'T sit well with a lot of people. That's what the Jackson-George Regional Library System board of trustees in Mississippi found out when it banned <i>Daily Show</i> host Jon Stewart's best-selling satirical book, <i>America (The Book): A Citizen's Guide to Democracy Inaction</i>.<p>
Released in September, the book has spent 15 weeks on the <i>New York Times </i>best-seller list and was named Book of the Year by <i>Publishers Weekly</i>.<p>
But the library board objected to one specific page--with images of naked Supreme Court justices. The facing page has cutouts of the justices' robes, with a caption asking readers to "restore their dignity by matching each justice with his or her respective robe."<p>
In response to the announcement that the book would be pulled from the shelves of eight libraries, the board was inundated with critical e-mails and phone calls, from both local and out-of-state residents. So, in a hasty decision, the board voted last week to restore <i>America</i> to the library system's shelves.<p>
Not all of the board members were in favor, however. Board member David Ogborn, who opposed lifting the ban, sniffed, "Our libraries are not a trash bin for pornographic materials." <br>
-- <b>Associated Press, </b>January 11, 2005<p>

<font face="Times New Roman, Times, serif" size="4"><b>Makeup--or be fired</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IT'S A woman's job to wear makeup, according to a federal appeals court.<p>
In December, the U.S. 9th Circuit Court of Appeals ruled that Darlene Jespersen, a female bartender who had worked for nearly 20 years at a Harrah's casino bar in Reno, Nev., could be fired for refusing to comply with the company's revised policy requiring female bartenders to wear makeup. A previously much-praised employee, Jespersen was fired in 2000 after Harrah's instituted a "Beverage Department Image Transformation." <p>
In a 2-1 decision, a three-judge--and all-male--panel upheld a lower court ruling in favor of Harrah's. "We have previously held that grooming and appearance standards that apply differently to women and men do not constitute discrimination on the basis of sex," Judge Wallace Tashima wrote for the majority.<p>
But as Judge Sidney Thomas wrote in the dissenting opinion, "Harrah's fired Jespersen because of her failure to confirm to sex stereotypes, which is discrimination based on sex and is therefore impermissible. The distinction created by the majority opinion leaves men and women in services industries, who are more likely to be subject to policies like the Harrah's 'Personal Best' policy, without the protection that white-collar professionals receive."<br>
-- <b>Reuters, </b>December 28, 2004<p>

