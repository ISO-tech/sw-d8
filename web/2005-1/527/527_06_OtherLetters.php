<p><a name="Medical"></a><font face="Times New Roman, Times, serif" size="4"><b>Medical needs of poor ignored</b></font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
While Michigan's food pantries remain understocked and school-age poverty levels remain high, an additional aspect to this reality for hundreds of thousands living in Michigan was the subject of an article in the <i>Muskegon Chronicle</i>: "Agencies struggle to meet medical needs of the poor."<p>
While the number of those living in poverty with medical needs has risen throughout 2004, a federal funding freeze for those living in extreme poverty went into effect July 1, 2004. At the time of the freeze, state aid was budgeted for 63,000 people. The numbers now exceed 89,000, leaving the state's various charities scrambling to meet the medical needs of the poor. <p>
Many, no doubt, are left in crisis situations when considering cancer and AIDS patients. Diana Stubbs, director of one of Muskegon's aid agencies, said that they remain "in dire need of donations if they are to continue." Diabetes and heart disease, both of which require medication, are common among the poor being helped in Michigan. <p>
But the delays in the paperwork that the charities are forced to do under a capitalist health care system can be life-threatening. <p>
Cheryl Schneider, of Muskegon Care, says "Many are homeless, living friend to friend. Thirty dollars for a prescription may not seem like much, but when you have no income it may as well be $300."<p>
The poorest of the poor, some seriously ill, also often homeless, living through the winter months in Michigan, are in urgent need of a common sense, humane solution to their problems.<br>
<b>Brian McAfee,</b> Muskegon Heights, Mich.<p>
