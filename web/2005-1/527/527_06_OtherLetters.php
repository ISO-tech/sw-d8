<font face="Times New Roman, Times, serif" size="4"><b>Not-so-friendly skies for us</b></font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
Thanks for your excellent article on the crisis in the airline industry ("Crunch time at airlines", January 7). Your analysis of the unions' failed strategy of givebacks and lack of solidarity is particularly apt; the pressing need is to defend working conditions and fight for some immediate pro-worker state intervention in the industry. <p>
I would add that unions and the left need to seriously consider nationalization of the air transportation system as well.<p>
Air transportation cannot be organized rationally by the so-called "free market." Scheduling flights efficiently is hard enough as a pure engineering problem without capitalists trying to rip off and kill each other at the same time. Perennial fare wars have created massive, chronic overcapacity (e.g., three third-full planes going to the same place at the same time) which is confusing, expensive and environmentally unsound. <p>
Nationalization could rationalize the system, thereby maintaining decent conditions for workers and affordable fares for consumers.<p>
The latter point is important, since deregulation did in fact lower fares; the old system, while regulated, was after all still designed to ensure juicy profits for private carriers. <p>
The corporations--who certainly want public money, just no public control--will pose as defenders of cheap, democratic air travel to win consumers to their side. By raising the slogan of nationalization, our side can show how the corporations are both anti-worker and anti-consumer.<p>
As a final thought, it is interesting to note that the author of American airline deregulation is none other than that "great" liberal, Democratic Sen. Ted Kennedy of Massachusetts.<br>
<b>Shaun Joseph,</b> Providence, R.I.<p>
