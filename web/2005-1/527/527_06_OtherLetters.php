<p><a name="Caterpillar"></a><font face="Times New Roman, Times, serif" size="4"><b>Caterpillar's complicity</b></font><p>

<b>Dear <i>Socialist Worker,</b></i> <br>
Human Rights Watch has said that Caterpillar Inc. should suspend sales of its D9 bulldozer to Israel's army. <p>
Caterpillar makes the D9 to military specifications and sells the bulldozers to Israel as weapons under the U.S. Foreign Military Sales Program. Once exported, the bulldozers are armored by the state-owned Israel Military Industries Ltd. The D9 is then used by the Israeli Defense Force (IDF) to demolish Palestinian homes, destroy agriculture and shred roads in violation of the laws of war. <p>
In the last four years, in the Gaza Strip alone, more than 2,500 Palestinian homes have been demolished by the IDF.<p>
The company's code of conduct states that "Caterpillar accepts the responsibilities of global citizenship," and it "must take into account social, economic, political and environmental" factors throughout its business operations. Sarah Leah Whitson, Middle East director at Human Rights Watch, is quoted as saying, "Caterpillar betrays its stated values when it sells bulldozers to Israel knowing that they are being used to illegally destroy Palestinian homes."<p>
It's about time global corporations are called to account for their complicity in war crimes.<br>
<b>Dave Edwards,</b> Doncaster, England<p>

