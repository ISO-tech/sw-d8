<p><a name="Left"></a><font face="Times New Roman, Times, serif" size="4"><b>What should the left do?</b></font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Dear <i>Socialist Worker,</b></i> <br>
The progressive forces in America have retreated into backing the Democratic Party. They thought that this was the best way to advance the social democratic reform agenda. But backing the Democrats also means all-out support for American imperialism.<p>
Kerry and his fellow Democrats voted for the Iraq war. He even said that he would continue the occupation until the job was finished--meaning when the oil was flowing. Are we progressives prepared for that?<p>
History is clear. The Democrats have started even more imperialist wars than the Republicans. Truman brought us the Cold War, McCarthyism and Korea. Kennedy and Johnson brought us the Bay of Pigs and Vietnam. Carter--Iran. Clinton--Yugoslavia and East Africa.<p>
When Howard Dean's peace movement was rising high, the Democratic National Committee moved in with John Kerry to cut off Dean and keep up support for the war. Really, is it worth supporting all this horror in the vague hope of getting a few small domestic reforms?<p>
American politics works like this: The Republicans own the upper middle class and the culturally backward. The Democrats have been carefully constructed by the ruling class to co-opt the disadvantaged--labor, ethnic minorities and women. They are awfully good at that job, awfully rich and strong, and totally incapable of being changed.<p>
What then are we progressives supposed to do? We must start again on the laborious task of building an alternative political force--a new party, down the road. Not right away, but when objective conditions (depression or losing war) send the people to the left. That will come, maybe sooner than we think. Meanwhile, let's keep away from the donkey's hind feet--unless we like getting kicked.<p>
Our natural base continues to be workers, women, minorities and environmentalists. Somehow blending them into a new movement--that is the only way to go. Forget about the Daley machines, the Zell Millers, the Libermans and the Kerrys. They are the enemy.<p>
All the above advice comes from the left. Now, let me grasp the third rail of political "correctness" and take note of two things from the right.<p>
First, we on the left must, of course, offer gays and lesbians all the benefits that heterosexual couples have in health, welfare and inheritance. But call it "civil union" or something--not "marriage." Recent referendums in 11 states against gay marriage passed by margins of over 80 percent.<p>
Second, face up to the reality of what degradation in the ghettos is doing to race relations. Join with Jesse Jackson, Barack Obama and Kwame Mfume in urging the Black population to pull itself up in the areas of crime, family stability and education.<p>
I regret that a lot of the above is going to annoy my long-time progressive friends. But let me ask you to calmly assess whether any of it is untrue.<br>
<b>Perry Cartwright,</b> Woodbridge, Ill.<p>
