<P><font face="Times New Roman, Times, serif" size="5"><b>Trying to silence Head Start critics</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 6, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">KEEP YOUR mouth shut--or else. That's the message going out from Washington to Head Start centers and staffers who speak out against the Bush administration's plans to wreck the preschool program for poor kids.</P>
<P>The threats began when the National Head Start Association, a lobbying group made up of Head Start staffers and families, began a "Save Head Start" campaign on April 16 to oppose the White House's proposal to turn over administration of the program to the states. Under Bush's proposal, governors could request their share of the money as a block grant to be spent at their discretion. States wouldn't be required to involve parents in the same way, nor would they be have to provide the same level of health care and social services to Head Start families. </P>
<P>The White House doesn't seem to take kindly to criticism. When the National Head Start Association sent its members a flier urging them to lobby members of Congress and alert local media, the Bush gang moved to shut them down.</P>
<P> A top federal Head Start official wrote centers to warn that federal law bans the use government money or staff time to lobby Congress--as well as ''grassroots lobbying concerning either federal or state legislation.'' In fact, the letters weren't sent using federal time or money.</P>
<P>Still, said Sarah Greene, president of the National Head Start Association, some frightened Head Start directors asked whether they should stop talking to parents about the proposed legislation. "It was an effort to chill the response from parents and those in the community who are opposed to what the Bush administration is proposing to do," she said. </P>
<P>As Ron Herndon, chairman of the National Head Start Association, told Associated Press: ''Head Start has been around for 38 years and no previous administration has ever seen fit to twist and contort the federal laws in order to justify what appears to be an unconstitutional attempt to silence the critics of its proposal."</P>



