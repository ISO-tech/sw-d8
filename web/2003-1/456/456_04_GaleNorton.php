<P><font face="Times New Roman, Times, serif" size="4">The Bush gang tries to swindle Native Americans</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Hypocrisy from the outset</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 6, 2003 | Page 4</font></p>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</I> </P>
</B><P>In a sickening move, Interior Secretary Gale Norton has tried to put a provision in the Interior Department's 2003 appropriations bill that would limit the pay of a court-appointed official investigating Norton's own obstruction of justice.</P>
<P>The case involves the Interior Department's mishandling of a trust fund for 500,000 Native Americans. The fund provides the Indians with royalties for mining, logging, grazing and oil drilling on Indian lands. Norton has already been cited for contempt of court for concealing information from a federal judge overseeing the case. </P>
<P>This same bill would make taxpayers foot the entire bill for private attorneys for 39 former or current government officials who have been accused of fraud and other misconduct. Criticizing Norton's action, U.S. District Judge Royce C. Lamberth wrote: "The appropriations provisions at issue in this matter appear to represent yet another attempt by defendants to evade the rule of law by any means available to them, no matter how duplicitous or underhanded. They also serve to demonstrate defendants manifest hypocrisy."</P>
<P>The Bush administration has also used the Justice Department to accuse Lamberth of violating the constitutional separation of powers between the executive and judicial branches. But as Lamberth commented, this "purported indignation stands in marked contrast to the alacrity with which defendants utilized the power of the executive branch to apparently persuade the legislative branch to undermine the effect of Judicial orders."</P>
<P>During testimony on May 17, Fiscal Assistant Treasury Secretary Donald Hammond admitted that nearly all his department's disbursement records for Individual Indian Trust accounts from the trust's inception in 1887 until 1992 have been destroyed. He told Lamberth that it will be impossible to account for all the funds that should have gone to trust accounts the government maintains for individual Indians.</P>
<P> But using satellite imagery, the plaintiffs say that they have determined how many oil wells are on Native American reservations. With that information, they have calculated how much revenue should have gone to the Indians, and have developed a similar method for determining revenues from timber, grazing and mineral leases on Indian lands.</P>
<P>As Dennis M. Gingold, lead attorney for the 500,000 Indians said: "This case has been about government hypocrisy and arrogance from the outset. It is a disgrace that the government would declare itself trustee for the nation's poorest people and then, 116 years later, obstruct every effort to provide those people with a full and fair accounting of their trust assets."</P>
<B><P>Evan Kornfeld, </B>Los Angeles</P>



