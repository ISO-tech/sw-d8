<P><font face="Times New Roman, Times, serif" size="4">Defying a crackdown</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Mass strike brings Peru to standstill</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 6, 2003 | Page 12</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THOUSANDS OF Peruvians defied a state of emergency to take to the streets last week in strikes and protests that have spread like wildfire across the country. Teachers began their national strike May 12, demanding higher wages.</P>
<P>On May 26, farmers--asking for lower taxes on their crops and import protections--joined the strike, bringing the transportation of goods to a halt by blocking major highways. In the central Andes, near the city of Jauja, seven police officers were injured after 2,000 farmers pelted them with stones when they tried to clear the highway.</P>
<P>On May 27, 35,000 doctors and nurses in eight government-run hospitals and 3,000 health centers walked out. Court workers and students also joined in the protests. That day, President Alejandro Toledo called a 30-day state of emergency to "restore order," banning strikes and demonstrations and giving the police and military the authority to use force and to enter strikers' homes with warrants.</P>
<P>Police clashed with protesters across the country. In the north, riot cops fired tear gas at about 5,000 striking teachers in Chiclayo. And in the south, soldiers shot at protesting students in Puno, killing one student and injuring at least 16 others. Despite this, protests continued through the end of May. </P>
<P>Farmers and health care workers called off their strikes this weekend, but teachers and court workers have not. "We teachers are not going to permit this kind of intimidation and aggression," Nilver Lopez, a leader of the Unico Union de Trabajadores in the Education of Peru (SUTEP), which represents 280,000 teachers across Peru, told Reuters.</P>
<P>A poll released on Friday showed that 71 percent of Peruvians supported the protests, reflecting the widespread anger with the Toledo government's inability to live up to its promises. Toledo, seen by many as an activist and reformer, took office in 2001, replacing the corrupt 10-year reign of Alberto Fujimori. Among his promises was to create 1 million jobs a year. Yet unemployment has stayed at 10 percent since he took office.</P>
<P>While Peru has been hailed as an economic success story--the country's 5.2 percent economic growth rate last year was the fastest in Latin America--none of this success has been felt by ordinary Peruvians. More than half the population of 27 million lives on $1.25 or less a day. Fifty-four percent of the population lives in poverty, and 24 percent are at half the official poverty line or less.</P>
<P>Strict restrictions on government spending demanded by the International Monetary Fund (IMF) and imposed by Toledo--a former World Bank adviser trained in the U.S.--have made conditions worse. Toledo's approval rating now stands at a miserable 14 percent.</P>
<P>Last June, Toledo called another state of emergency in response to massive demonstrations in Arequipa in the south against the privatization of two electric utility companies, as prescribed by the IMF. When martial law couldn't stop the protests, the government postponed its privatization plans indefinitely.</P>
<P>While there is great anger about Toledo's policies among workers, farmers and the poor, some people are happy with the president's performance. "It's the best showing by Toledo so far," Peruvian business leader Samuel Gleiser said of the declaration of martial law last week. "He took the bull by the horns."</P>
<P>Toledo's troubles are far from over. Peru's biggest umbrella union, CGTP, announced last week that it was considering calling a general strike in July against free-market policies.</P>
<P>The whole of Latin America has seen a rising tide of struggle. In February, Bolivian workers organized huge demonstrations against government austerity measures. And in December 2001, former Argentine President Fernando de la Rua was forced to resign after he declared martial law in the face of militant protests.</P>
<p>As high school teacher Jorge Vargas said at a protest in the capital: "If the government doesn't change its policy of kneeling down before the IMF...if it does not look the Peruvian people in the face...it's going to have to go."</p>

