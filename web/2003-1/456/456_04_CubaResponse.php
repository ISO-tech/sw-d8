<P><font face="Times New Roman, Times, serif" size="5"><b>Supporting Cuba's right to self-determination</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 6, 2003 | Page 4</font></p>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</P>
</B></I><P>Peter Cogan's letter (May 30) raises some legitimate concerns about my previous article on Cuba ("What's behind Cuba's crackdown,"<I> </I>May 16). Unfortunately, due to space limitations, there were several important issues that I could not address.</P>
<P>I had no intention of lumping the Cuban government's response to the dissidents with the executions of the three hijackers. They are quite separate incidents, and I certainly oppose the executions.</P>
<P>Yet there is a connection--because the U.S. has instigated an immigration bottleneck that has led more ordinary Cubans into desperate acts. With more than half of the current fiscal year over, the U.S. has honored only some 2,200 of the minimum 20,000 annual visas agreed to during the Clinton administration.</P>
<P>We believe that people should be free to move across national borders. It would be a scandal if, for example, Mexican President Vicente Fox began to execute Mexicans who illegally cross the U.S. border in order to avoid a mass migration crisis--which is what Castro has claimed as a rationale for the three executions.</P>
<P>This clearly highlights that there is something fundamentally wrong with the Cuban regime--that Cuba is not a socialist society run by its working people. We believe that its ruling clique should be taken care of by a mass movement--of the sort that Cogan refers to when he brings up the example of Poland's 10 million-strong Solidarnosc trade union. It was a massive movement that formed embryos of workers councils and formulated radical working-class demands.</P>
<P>Though some in the Solidarnosc leadership may have received support from the U.S. government, it would be a grotesque distortion to suggest that the 10 million workers of Solidarnosc were all in the U.S. pocket. And that is the difference with the current crop of Cuban dissidents.</P>
<P>They were groomed by the U.S. Agency for International Development to the tune of millions of dollars, and their latest stage manager is James Cason, the head of the U.S. Interests Section in La Habana. You cannot claim to be fighting for democracy while giving away one of the most fundamental democratic rights, the right to self-determination.</P>
<B><P>H&eacute;ctor Reyes, </B>Chicago</P>



