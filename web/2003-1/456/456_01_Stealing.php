<P><font face="Times New Roman, Times, serif" size="4">Celebrating their latest tax cut theft</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>They're stealing our future</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 6, 2003 | Page 1</font></p>

<P><font face="Times New Roman, Times, serif" size="3">CREATING JOBS. Making the tax system fairer. Helping U.S. families suffering from the economic slump. That was all a pack of lies.</P>
<P>The real purpose of George W. Bush's latest tax cut giveaway was made crystal clear when Republican leaders of Congress--and the White House hacks they worked with every step of the way--used a last-minute change in the fine print to deny tax breaks to millions of families with an income at or just above the minimum wage.</P>
<P>These households won't get the $400 increase in the child tax credit--money that will be sent by check to those eligible by the end of the summer in what was supposed to be a broad-based effort to kick-start the economy by getting money into the hands of consumers. Not certain consumers, apparently.</P>
<P>A further look at the fine print by two liberal think tanks revealed an additional 8 million taxpayers who lost any benefits they might have gotten when a congressional committee met behind closed doors to come up with the final bill. All told, there are 50 million households--more than one in three across the U.S., almost all of them low income--that won't get a penny from Bush's tax law.</P>
<P>But for the already super-rich, Washington has come up with another gift--one that will keep on giving. The centerpiece of the legislation is reductions in stock dividend and capital gains taxes paid almost entirely by people with the highest incomes. People like...well, Dick Cheney, who stands to rake in an extra $100,000 a year or more.</P>
<P>"Perhaps now," wrote <I>Boston Globe </I>columnist Thomas Oliphant, "a few people might begin to recognize that the real class war in the United States all along has been waged against those who work full time for modest wages, and that the goodies for the rich are literally coming out of their pockets."</P>
<P>Embarrassed by press reports that identified the losers in the tax bill, Republicans tried to pass the buck. "Apparently," sniffed Christin Tinsworth, spokesperson for Republicans on the House Ways and Means Committee, "whatever we do is not going to be enough for some segments of the population."</P>
<P>The GOP blamed Democrats for demanding that the final legislation cost no more than $350 billion. What choice did lawmakers have but to dump the low-income families--to save $3.5 billion, or 1 percent of the final price tag?</P>
<P>Meanwhile, congressional negotiators also stripped the final legislation of provisions in the Senate version that would have closed a few outrageous corporate tax loopholes, like setting up in offshore tax havens. That would have saved $25 billion, seven times more than what was needed to fund the child tax credit. But helping out their corporate pals was more important to the White House than the working poor.</P>
<P>The Bush gang's determination to steal from working people to give to the rich couldn't be more obvious. But these con artists have another aim in mind as well.</P>
<P>By forcing through the second- and third-largest tax cut giveaways in U.S. history since 2001, the administration is putting the federal government in a financial bind for decades to come. Politicians will use the massive government budget deficit as a stick to beat back demands for increased spending.</P>
<P>Unless the tax cuts are repealed, it is only a matter of time before Washington starts making drastic cuts in programs that working people depend on the most--Social Security, Medicare and Medicaid. Already, the impact can be felt. For example, the administration has already cut funding for education promised under Bush's much-hyped Leave No Child Behind Act.</P>
<P>State governments, which are already facing the worst fiscal crisis in 50 years, will be left holding the bag--or, more accurately, wielding the budget ax. In Oklahoma, for example, officials believe that up to 40 percent of programs required by the Leave No Child Behind law are underfunded. "We're going to be so ill-equipped to meet those standards it's frightening," said Stacy Martin, of the Oklahoma Education Association. "Among Oklahoma teachers, the Bush scheme is jokingly referred to as 'No School Left Standing.'"</P>
<P>This shows why the Democrats' opposition to the Bush tax cut has been entirely inadequate. By and large, leaders of the "party of the people" accept that some tax cuts are necessary--when what's really needed is tax <I>increases </I>on the super-rich who have made out like bandits.</P>
<P>We can't let Bush get away with stealing our future. We have to expose the truth--and build the kind of grassroots struggles that can present a real challenge to Washington's tax cut looters.</P>



