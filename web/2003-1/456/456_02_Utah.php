<P><font face="Times New Roman, Times, serif" size="5"><b>Execution by shooting spree</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 6, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">UTAH PRISON officials have put out the call. Help wanted: Sharpshooters needed to execute two death row prisoners on back-to-back days in late June. Only those seeking the opportunity to kill with the full backing of the law need apply.</P>
<P>Utah is one of three states that allows for the death penalty to be carried out by a firing squad--and it's the only one to have carried this grisly form of execution since the reinstatement of capital punishment a quarter century ago. Troy Michael Kell's execution is scheduled for June 27, and Roberto Arguelles faces the same fate on June 28. Kell is expected to be granted a stay as he pursues a new round of appeals.</P>
<P>Utah's planned "double execution by shooting spree" is making international headlines. Death penalty supporters are worried--because they fear the spectacle could tarnish the sterile image "civilized means" of executions, such as lethal injection.</P>
<P>Sheryl Allen--a pro-death penalty Republican legislator--may even introduce legislation banning death by firing squad. But the death penalty system--riddled as it is by racism, wrongful convictions and a bias against the poor--is barbaric regardless of the method used to carry out the act of state-sponsored killing.</P>
<P>"A look around the world finds the death penalty on the retreat," admits the <I>Salt Lake Tribune</I> in a pro-death penalty editorial. "Long-standing democracies consider capital punishment a relic of an uncivilized past. Newly free nations often rush to abolish executions, remembering them as the often-abused tool of their late oppressors.</P>
<P>"Even in the United States, the tide is slowly turning. Too many stories of death row inmates who were placed there by police misconduct or due to plainly incompetent defense counsel have led to moratoriums on executions and, early this year in Illinois, a mass commutation of death sentences. </P>
<P>"The firing squad's bloody form of execution--just wait until one is botched by a couple of sweaty-fingered marksmen--moves those who might otherwise be unconcerned to oppose capital punishment in all its forms."</P>



