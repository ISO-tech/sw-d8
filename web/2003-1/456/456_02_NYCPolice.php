<P><font face="Times New Roman, Times, serif" size="4">Killings spark new round of police brutality protests</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Victims of the NYPD</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sarah Hines </b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 6, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">ALBERTA SPRUILL was a 57-year-old city worker, a member of AFSCME District Council 37 and an avid churchgoer. Ousmane Zongo was a 35-year-old West African immigrant who repaired art objects in a storage facility in Chelsea. Yet these two different people have a lot in common. </P>
<P>Both were respected members of their communities in New York City. Both were Black. And both fell victim to the terror of the NYPD within a single week. Now, their deaths are sparking a new round of anti-police brutality protests.</P>
<P>Spruill was literally scared to death--dying of heart failure after police broke down her door and set off a concussion grenade in a mistaken raid May 16. In an unusually candid report released last week, the NYPD admitted that police failed to monitor Spruill's apartment before the raid, didn't inform the officers involved that the drug dealer they were looking for was already in custody, and knew that the informant who led them to Spruill's address had proven unreliable on previous occasions. Still, Police Commissioner Raymond Kelly told reporters that he didn't think this represented an "inordinate number" of mistakes.</P>
<P>Less than a week after Spruill died, the cops killed Zongo, shooting the unarmed man four times, once in the back. Once again, Zongo had nothing to do with the crime that police were investigating--a CD-counterfeiting operation based in the same warehouse where he worked.</P>
<P>The truth is that these two deaths were not accidents. They were the result of the NYPD's shoot-first-and-ask-questions-later policy when it comes to people of color. As a representative of the civil rights group, the National Action Network, said, "The one thing that was known to those officers was that behind that door was an African-American or a Latino." </P>
<P>These horrible killings show the crying need for a movement that can challenge police violence. After the killing of Amadou Diallo--another unarmed African immigrant who died in a hail of 41 police bullets in 1999--daily protests grew large enough to put pressure on the NYPD. Arrest rates and police killings declined significantly. Unfortunately, that movement has slowed down while the NYPD has carried on its dirty work.</P>
<P>Still, last month's killings sparked several protests that, while small, have grown in numbers. About 200 people gathered outside of Spruill's apartment building May 27 and marched to her church a few blocks away. There was a clear sense of anger that turned what had been planned as a quiet vigil into a loud protest march.</P>
<P>Rev. Al Sharpton called on people at the rally to "stand together, fight together and win together." "We need to really deal with this as a systematic problem," Sharpton told <I>Socialist Worker</I>. "It's going to take everything from street mobilization to pressuring the courts." </P>
<P>Calvin Alston, vice president of the tenants' association that Spruill belonged to, said that Mayor Michael Bloomberg's apology wasn't enough. "All they say to us is, 'Oh, we're sorry,'" Alston said. "But we're the ones who have to pick up our lives all over again." </P>
<P>In response to Zongo's murder, about 100 people gathered May 28 at the storage facility where he was killed. The demonstration was almost entirely made up of African immigrants and was organized by immigrant community leaders and the National Action Network.</P>
<P>Now, several organizations, including the Coalition of Black Trade Unionists, Spruill's church and the National Action Network, are planning for a June 21 protest at City Hall at 11 a.m. This demonstration will be an important opportunity to begin rebuilding an anti-police brutality movement that is strong enough to fight and win. </P>

<I><P>Laura Durkay contributed to this report.</P>


