<P><font face="Times New Roman, Times, serif" size="4">Loaded with gimmicks but short on plot</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Matrix II doesn't measure up</b></font></P>

<B><I><P><font face="Arial, Helvetica, sans-serif" size="2">The Matrix Reloaded, </B></I>directed by Andy and Larry Wachowski, starring Keanu Reeves, Laurence Fishburne and Carrie-Anne Moss.</font></P>

<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Hadas Thier and Laura Durkay</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 6, 2003 | Page 9</font></P>

<P><font face="Times New Roman, Times, serif" size="3">EVER FEEL as though you are just a cog in the machine, controlled by a system as unfeeling as it is brutal? According to <I>The Matrix </I>and its recent sequel, <I>The Matrix: Reloaded, </I>you are.</P>
<P>The original <I>Matrix </I>made millions at the box office because of its eye-popping special effects and a simple but fascinating premise: that the world we think we live in is an illusion. In the world of the Matrix, machines have created a near-perfect system of exploitation: they use humans as batteries, the ultimate renewable energy source.</P>
<P>Human beings--not content to accept their fate as lifelong Duracells--are kept under control by being "plugged in" to a computer program called the Matrix, which resembles the world at the end of the 20th century. But even this system is not infallible, and a small number of humans have been able to learn the truth and build a resistance movement.</P>
<P>As Morpheus, one of the leaders of the resistance, explains in the first movie: "The Matrix is everywhere...You can see it when you look out your window or when you turn on your television. You can feel it when you go to work, when you go to church, when you pay your taxes. It is the world that has been pulled over your eyes to blind you from the truth...That you are a slave...born into bondage, born into a prison that you cannot smell or taste or touch. A prison for your mind."</P>
<P>In <I>The Matrix, </I>knowing the truth is the key to beating the system--and the truth is always preferable to a lie, even when the real world is dirty, dangerous and uncomfortable. In this way, <I>The Matrix </I>takes up the themes of exploitation, consciousness and radicalization. In the more ambiguous world of science fiction, Hollywood can find <I>The Matrix </I>acceptable.</P>
<I><P>The Matrix: Reloaded, </I>the much-hyped sequel, follows the machines' attempt to destroy Zion, the last stronghold of free humans. Some of themes of the first movie are developed and explored. We learn more about Zion, the Matrix itself and the character of Neo, who is "the One," a human born into the Matrix with a unique ability to transform and resist it.</P>
<P>Writers-directors Andy and Larry Wachowski play around with some interesting concepts in their virtual world, like rogue programs and "back doors" written into the Matrix code They further develop the mysterious Agent Smith, a Matrix-created police agent who has learned to act independently. In its own very philosophical way, the film also deals with consciousness, free will and choice, and materialism versus idealism. And of course, the special effects remain incredible.</P>
<P>But ultimately, <I>Reloaded </I>falls short. Besides trying to pack in too many gimmicks and absurdly long fight scenes, <I>Reloaded </I>misses the opportunity to explore resistance and collective struggle among the free human population of Zion.</P>
<P>If the revolution happened tomorrow, we're told in the first Matrix, "Zion's where the party would be." The population of Zion is realistically multiracial and multi-ethnic. Neo may be "the One," but he gets no special quarters.</P>
<P>Still, though Zion is supposedly a city full of rebels, most of its citizens end up as passive observers, who leave the resistance to a small crew of elite fighters. This, of course, falls in line with Hollywood's status quo vision of where liberation comes from--a select, talented few.</P>
<P>The best part of the movie--apart from the opening scene in which the Matrix code turns into a time clock--is the character of Trinity, who helped rescue Neo from the Matrix in the first film. As his partner in this movie, she stays both strong and passionate, and her relationship with Neo remains solid.</P>
<I><P>The Matrix: Reloaded </I>is worth watching, but we hope the last and final movie in the trilogy, <I>The Matrix: Revolutions, </I>ties up the loose ends and lives up to the concept's full potential.</P>


