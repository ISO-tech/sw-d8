<P><font face="Times New Roman, Times, serif" size="4">Reform leaders of Chicago teachers in contract fight</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>CTU needs a battle plan</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jesse Sharkey, Delegate, CTU</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | June 6, 2003 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--Some 35,000 members of the Chicago Teachers Union (CTU) face a fight for a new contract as their current deal expires June 30. For CTU President Debbie Lynch and the new reform union leadership--elected two years ago as a rebuke to the union's old-guard United Progressive Caucus--the contract represents a crucial test.</P>
<P>The question is whether Lynch and her Pro-Active Chicago Teachers (PACT) caucus mobilize the union for a fight, or reach their own accommodation with city hall. In other words, will Lynch live up to her election promise of building a more powerful, self-reliant union, or will she follow the example of former President Tom Reece, who turned the union from teachers' watchdog into a lapdog?</P>
<P>An explosive mix of factors surrounds these negotiations. While the Board of Education is crying poverty, a decade of pent-up frustrations, pay-freezes and cutbacks have teachers fighting mad.</P>
<P>Already, the board has closed five schools and eliminated 300 non-union administrative positions. "Expenses are up, revenue is down, it [the contact] is going to be a bear," said the board's chief negotiator, James Franczek.</P>
<P>Unfortunately, union leadership has been quiet about contract negotiations. "We will not negotiate this contract in the press," Lynch told delegates. The union has done little to take the case for a fair contract to its members or the public. So far there have been no press conferences, no informational pickets, no visits to local school councils or even leaflets to members highlighting our demands.</P>
<P>Our case should be easy to make. Once the highest paid teachers in the state, our pay has fallen so that only 3 percent of area teachers are paid less. And in view of massive city pork projects, including $370 million for Millennium Park and $387 million for Soldier Field, mean that few people believe that there is no money available for things that Mayor Richard Daley and Co. find important.</P>
<P>What's more, Chicago property tax values have soared over the past decade--from about $90 billion in 1993 to $160 billion today. Even the school board's own Comprehensive Annual Financial Report states that "in general the financial operations of [Chicago Public Schools] have performed well."</P>
<P>Meanwhile, the federal education law, No Child Left Behind, has meant a significant requirement for "up-skilling" for teachers, including expensive re-certification. Not only must Chicago teachers pay for their own professional training, we are paid $30,000 a year less than the suburbs at the top of our pay scale--despite our more difficult conditions.</P>
<P>However, Daley and the Chicago Public Schools bureaucrats have gotten used to treating Chicago teachers like a doormat. The previous contract granted raises of 0.8 percent below inflation--and that was on top of a decade of concessions. </P>
<P>PACT now faces a dilemma which could split the left and right within the caucus. Does it support Lynch's effort to rely on good relations with Chicago Public Schools CEO Arne Duncan to quietly negotiate a contract? Or does it go to the union's members and mobilize a fight that may well lead to a strike?</P>
<P>If PACT brings a poor contract back to the membership, it would risk losing the next election. This would be a tragedy that would set the union back a decade. This is why all activists in the union need to make mobilizing for a contract fight a full-time job.</P>


