<P><font face="Times New Roman, Times, serif" size="5"><b>Will Camp X-Ray get a new death row?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 6, 2003 | Page 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3">THE BUSH administration is floating plans to add a death row and an execution chamber to its prison camp in Guant&aacute;namo Bay, Cuba. Britain's <I>Mail on Sunday</I> newspaper reported that prisoners would be tried, convicted and executed without leaving its boundaries, without a jury or right of appeal.</P>
<P>The plans were revealed by Major Gen. Geoffrey Miller, who runs the camp and oversees 680 prisoners from 43 countries--including at least four children between 13 and 16 years old. Miller said building a death row was one plan. Another was to have a permanent jail, with an execution chamber possibly built in. </P>
<P>The Bush administration still refuses to classify the captives at Camp Delta as "prisoners of war" under the Geneva Convention. "The U.S. is kicking and screaming against any pressure to conform with British or any other kind of international justice," British activist Stephen Jakobi of Fair Trials Abroad told the <I>Mail on Sunday.</P>
</I><P>As Georgetown University law professor Jonathan Turley told Australia's <I>Herald Sun</I>: "It is not surprising the authorities are building a death row because they have said they plan to try capital cases before these tribunals. "This camp was created to execute people. The administration has no interest in long-term prison sentences for people it regards as hard-core terrorists." </P>
<P>But in a sickening twist, the Bush gang is now in the position of trying to save some of the prisoners they may later execute. Just last week, Pentagon officials admitted that two more prisoners at the Camp Delta attempted suicide--bringing the number of suicide attempts since prisoners began arriving to 27.</P>


