<p><a name="Equity"></a><font face="Times New Roman, Times, serif" size="4"><b>Equity Office Properties Trust</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Lauren Fleer and Mark Dickman</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">CHICAGO--A group of 150 janitors with Service Employees International Union (SEIU) Local 1 and their supporters picketed a May 20 meeting of Equity Office Properties Trust shareholders. Equity is the nation's largest office building owner and real estate investment trust (REIT) in the United States with more than 700 properties totaling more than 125 million square feet.</P>
<P>The picketers came to protest Equity's recent elimination of 68 jobs. Chanting "No more cuts!" and "We're fired up, we can't take no more!" the demonstrators handed out leaflets, raised banners and carried brooms along with placards reading "Equity Office--Chicago's dirty secret."</P>
<P>While Equity CEO Richard Kincaid takes home a six- or seven-figure income, Equity has recently implemented a plan to save $75 to $100 million annually by getting rid of 20 percent of its workforce. Equity aims to eliminate 420 jobs by the end of 2003--but SEIU janitors are not having it.</P>
<P>Equity's initial 68 layoffs impacted not only the workers whose jobs were cut, but also the workers left behind to take up the slack. Some janitors who regularly clean 35,000 to 40,000 square feet per shift are now expected to cover almost twice the same ground. Equity janitors are now having to work twice as hard--and won't see a penny of the rewards.</P>
<P>These moves have proven unpopular with Equity's tenants as well, who notice the decline in service quality. Through educating and organizing Equity's tenants, SEIU has managed to win back 35 percent of the slashed jobs. And they won't stop there. </P>
<P>June 12 will mark the kick-off of a nationwide week of activity for SEIU's Justice for Janitors campaign. Large downtown demonstrations June 14 will demand that Equity reinstate its laid-off workers and, above all, stop the cuts!</P>



