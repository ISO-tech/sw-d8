<p><font face="Times New Roman, Times, serif" size="4"><b>Oakland, Calif., teachers</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Jessie Muldoon, Oakland Education Association</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">OAKLAND, Calif.--Teachers in Oakland are fighting against deep cuts and layoffs. The state of California is in the process of loaning the Oakland Unified School District $100 million, which will come at the cost of a state takeover.</P>
<P>The school board has scapegoated teachers to distract attention from their own financial mismanagement that made the crisis  worse. Unfortunately, leaders of the Oakland Education Association (OEA) recently missed an opportunity to expose the school board for its dirty tactics.</P>
<P>The OEA had called a day of action May 28 at the school board meeting--but the board moved the meeting's location at the last minute. Despite that the OEA had made the call for the action, none of the elected officers or bargaining team members, and only two executive board members, turned out.</P>
<P>This is a step backward from the previous board meeting, when angry union activists representing teachers, food service workers, custodians and many other school employees shut the meeting down with a loud, confident rally. The failure of the May rally illuminated the need for rank and file organization that doesn't depend on the officials to fight such cuts.</P>
<P>As the school year comes to a close, activists need to hammer out a plan to organize throughout the summer, and hit the ground running when the school year begins. This will be a fight that will shape education and working conditions in Oakland for years to come.</P>

