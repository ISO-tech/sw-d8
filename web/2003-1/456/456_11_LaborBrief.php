<p><a name="Berkeley"></a><font face="Times New Roman, Times, serif" size="4"><b>Berkeley, Calif., teachers</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Jean Whittlesey, Berkeley Federation of Teachers</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">BERKELEY, Calif.--At an emergency meeting May 13, the Berkeley school board heard from angry teachers and parents outraged over layoffs. A week later, a district wide rally at the school administration building was billed as a unifying action for the four unions totaling some 1,000 teachers, secretaries, janitors, teaching assistants and support staff. It drew 125 people, half of them teachers.</P>
<P>Unfortunately, the union officials who spoke, including Berkeley Federation of Teachers  President Barry Fike, all emphasized that some layoffs were inevitable.</P>
<P>The fight began March 15, when 235 out of 590 teachers in Berkeley--40 percent of the total--received layoff notices.The pink slips resulted from both state budget cuts and previous district finances. Between 78 and 128 are expected to remain on layoff after  June 1 reversals are announced. </P>
<P>The cuts would have been worse if teachers and parents hadn't organized. The highlight of the fight was two layoff hearings, demanded by BFT and held on April 20 and 21.</P>
<P>They turned into loud, boisterous rallies organized by teachers and the PTA from several school sites and supported by University of California-Berkeley students. At the start of each session, up to 150 people marched into the hearings chanting "They say cutback, we say fight back!" and "No justice, no peace!"</P>
<P>BFT demanded the hearings to insure that the district followed seniority and teachers' credentials in the layoff process. However, the judge's decision allowed the district to proceed as planned except for one teacher who was recalled.</P>
<P>At the Board May 13 meeting, the board increased its list of rescinded layoffs from 12 to 50, a small victory for the teachers. Teachers need to organize the entire rank and file to stand together for no cuts, without apology. We will not stand by and see public education destroyed.</P>

