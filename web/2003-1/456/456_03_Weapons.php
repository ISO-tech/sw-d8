<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Unraveling the lies they told to get their war on Iraq</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bush's weapons of mass deception</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">June 6, 2003 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">"THE DISARMING of Iraq has begun." These were the words that the Bush administration used in March to announce that its war on Iraq had begun.</P>
<P>Two months later, the U.S. has declared victory, and it has the United Nations' approval to occupy Iraq. Just one thing: Still no weapons of mass destruction.</P>
<P>Despite "discoveries" throughout the war that the media hyped as "perhaps the smoking gun," U.S. forces have yet to find any hard evidence of weapons of mass destruction. Bush was reduced last week to claiming that the discovery of two trailers in Iraq--which U.S. officials insist, on the flimsiest of evidence, were mobile factories for producing chemical weapons components--was the same thing as discovering actual weapons. Now, to make matters worse for Bush and Co., evidence is coming to light that the supposed "proof" of Saddam Hussein's arsenal--which administration officials talked about constantly before the war--was at least partially fabricated.</P>
<P>Last September, British Prime Minister Tony Blair presented a 173-page intelligence "dossier" stating that Iraq could deploy chemical and biological weapons "within 45 minutes." But intelligence officials told the BBC last week that, before it presented the dossier, the Blair government ordered the documents to be "transformed."</P>
<P>"That information was not in the original draft," a senior intelligence official told the BBC. "It was included in the dossier against our wishes." Likewise, Bush administration officials were determined to find a smoking gun against Iraq--and dissatisfied with the CIA for failing to come up with the goods.</P>
<P>So in the fall of 2001, Defense Secretary Donald Rumsfeld started a separate intelligence agency called the Office of Special Plans (OSP)--and put his right-hand man Paul Wolfowitz in charge. Former CIA analyst Ray McGovern told the BBC that OSP claims about weapons of mass destruction in Iraq were "an intelligence fiasco of monumental proportions."</P>
<P>But the gamble was worth it. Without it, the Bush administration wouldn't have gotten its war on Iraq. "The truth is that for reasons that have a lot to do with the U.S. government bureaucracy," Wolfowitz explained in an interview with <I>Vanity Fair, </I>"we settled on the one issue that everyone could agree on, which was weapons of mass destruction."</P>
<P>In response to these revelations, some congressional leaders are calling for an investigation. But most Democratic leaders seem willing to let the White House off the hook.</P>
<P>Then there's the other lie--one that Congress definitely won't investigate--about bringing democracy to Iraq. The mask was off earlier this week when U.S. occupiers announced plans to select 25 to 30 Iraqis to lead an interim government--thus avoiding the messy business of holding democratic elections. Also this week, U.S. forces began the mandatory disarmament of Iraqi citizens, with soldiers barging into and searching homes--in the latest in a series of U.S.-sponsored humiliations.</P>
<P>The U.S. added to its list of Iraqi civilian victims last week when soldiers opened fire on a wedding celebration in the town of Samarra, killing three people and wounding seven, three of them "very critically," according to doctors. Atrocities like these have stoked a furious opposition to the occupation--including armed attacks on U.S. troops.</P>
<P>At the beginning of this week, four U.S. soldiers were killed and several wounded in two days of attacks near Falluja--where U.S. troops killed 15 protesters in April--and assaults on convoys in Baghdad and Qaim. Pentagon officials blame loyalists of Saddam Hussein's government, but the brewing anger in Iraq goes much deeper.</P>
<P>"We're against the occupation, we refuse the occupation--not 100 percent, but 1,000 percent," Mohammed Abdullah told a reporter as a U.S. patrol passed his home in Baghdad. "They're walking over my heart." This opposition will only grow as Washington's ugly occupation of Iraq continues--and as the lies that the Bush gang used as an excuse to invade in the first place become even harder to ignore.</P>



