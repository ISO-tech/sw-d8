<P><font face="Times New Roman, Times, serif" size="5"><b>Top cops caught in S.F. cover-up</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Terzakis</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 7, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>A GRAND jury in San Francisco has indicted the entire command staff of the city's police department for conspiring to obstruct an investigation into police brutality. The chief of police, assistant chief, two deputy chiefs and a half dozen others are charged with blocking an investigation into an assault by three off-duty cops.</P>
<P>The three cops involved in the attack--Alex Fagan, Jr., Matthew Tonsing and David Lee--face felony assault charges for brutally beating two men last November. The two victims were leaving a bar in the ritzy Union Street neighborhood when the three officers grabbed them and demanded their takeout food--and then assaulted them.</P>
<P>Turns out that Fagan, Tonsing and Lee had just come from a party--where 100 cops had celebrated the promotion of Fagan's father, Alex Fagan Sr., to assistant chief of police. Things only get more sordid from there.</P>
<P>The cops who responded to a 911 call from Snyder's cell phone allowed the three officers to leave without testing them for blood alcohol levels or checking their clothing for evidence. Later, a cop who was apparently leading the police investigation too aggressively was transferred to an undesirable position in the vice squad.</P>
<P>Any belief that police brutality is a matter of "a few bad apples" has been discredited. The SFPD has been exposed as rotten to the core.</P>
<P>Some city officials and leaders of the local NAACP chapter claim that District Attorney Terence Hallinan, who is white, is playing racial politics against Police Chief Earl Sanders and Mayor Willie Brown, who are Black. But the indicted group is as multiracial as the city itself, and as PoliceWatch activist Ishmael Tarikh told reporters, "We are very cognizant that there's only one color police officer, and that's blue."</P>
<P>For his part, Willie Brown has refused to allow any of the top brass to be suspended, claiming that this was "a matter of public safety." Clearly, Brown's definition of safety doesn't include the victims of police beatings.</P>


