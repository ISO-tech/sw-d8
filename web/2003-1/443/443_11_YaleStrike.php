<P><font face="Times New Roman, Times, serif" size="5"><b>Yale workers on strike</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sam Bernstein</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 7, 2003 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW HAVEN, Conn.--About 5,000 workers at Yale University walked out March 3, the first day of a planned five-day strike.</P>
<P>Yale Corp. refuses to give an inch in response to recent proposals by Hotel Employees and Restaurant Employees (HERE) Locals 34 and 35, which represent dining hall, service, maintenance, clerical and technical workers.</P>
<P>At issue are basic workers' rights--wages, pensions, affordable healthcare, childcare, respect on the job and even the right to unionize. Demands include union recognition for Yale-New Haven Hospital workers, who are attempting to organize with Service Employees International Union (SEIU) Local 1199, and graduate teaching assistants, who have already voted to unionize.</P>
<P>The unions are also demanding that Yale, with its $10.7 billion endowment, contribute more to New Haven--the fourth-poorest city in the nation--through educational subsidies, home buying programs and access to better jobs.</P>
<P>The strike has already won widespread solidarity, with labor leaders such as AFL-CIO President John Sweeney and SEIU President Andrew Stern scheduled to speak, along with Rev. Jesse Jackson and Princeton University Professor Cornel West.</P>
<P>Since January 2002, when the original contracts expired, the unions have renewed them each month and kept bargaining. But the contracts contain a no-strike clause that has prevented the unions from taking action--despite a September 4 vote that was overwhelmingly in favor of a strike.</P>
<P>For the last two months, Yale has even refused to come to the bargaining table. In the face of these attacks, rank-and-file workers have demanded more serious action.</P>
<P>On February 14, HERE Locals 34 and 35 announced that they wouldn't renew their contracts and called for a five-day strike beginning March 3. Days later, graduate student employees voted by a 4-to-1 margin to authorize a solidarity strike.</P>
<P>As <I>Socialist Worker</I> went to press, workers had set up picket lines and teach-ins to shut down business as usual at Yale. In a show of solidarity, many professors moved their classes off campus so that students would not have to cross picket lines in order to attend class.</P>
<P>This strike is a step forward for workers at Yale. It's time for Yale workers to get the contracts and respect they deserve!</P>


