<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Twisting arms and cutting deals</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Washington's power politics</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 7, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>VOTE AGAIN until you get it right--so we can have our war just the way we want it. That was the message from Washington last weekend after Turkey's parliament voted against allowing some 62,000 U.S. troops into the country as part of the Pentagon's plans for invading neighboring Iraq.</P>
<P>Under pressure from Washington--and a bribe of $30 billion in aid--the Turkish government was trying to bring the issue to a second vote as <I>Socialist Worker</I> went to press. And George W. Bush dares to claim that his war will bring democracy to the Middle East!</P>
<P>These ugly power politics highlight Washington's real war aims--not to disarm Iraq, but to finally establish the U.S.-run "new world order" that George Bush Sr. declared after the last Gulf War.</P>
<P>Whatever the outcome in Turkey, the first vote has already stunned Washington--and showed how "unwilling some in President Bush's 'coalition of the willing' are turning out to be," the <I>Wall Street Journal</I> observed. Some 100,000 antiwar protesters surrounded the Turkish parliament, highlighting the fact that more than 90 percent of the country opposes a U.S. war.</P>
<P>The White House thought it had closed the deal after it promised Turkey's military that it could send troops into Iraqi Kurdistan, where a virtually independent state has been built up since the 1991 Gulf War.</P>
<P>Turkey's Foreign Minister Yasar Yakis has demanded that the U.S. guarantee that the 4 million Kurds of Iraq would not be allowed to declare independence--for fear that this would set an example to the 15 million Kurds in Turkey, the victims of a genocidal war waged by that government for more than two decades.</P>
<P>Under the deal that Washington offered, tens of thousands of Turkish troops could invade oil-rich northern Iraq. That would almost certainly lead to fighting between the Turkish military and Kurdish forces in Iraq--and make a mockery of U.S. claims to support Kurdish rights.</P>
<P>In fact, Washington has promised to support Kurdish uprisings before, in 1975 and again at the end of the 1991 Gulf War--only to betray them. And if it has to betray them again to buy Turkish support for war, it won't hesitate.</P>
<P>Meanwhile U.S. officials will continue twisting arms and trying to cut backroom deals to win the support of France, Germany, Russia and China for a United Nations (UN) resolution sanctioning the war.</P>
<P>But it's clear that Washington is ready to go it alone, with or without UN approval. That's why the U.S. unilaterally added the demand that Saddam Hussein be exiled as a condition of averting war--to show that Washington sets the agenda, not the UN.</P>
<P>These strong-armed tactics prompted an establishment figure--John Brady Kiesling, a U.S. career Foreign Service officer stationed in Greece--to quit last week. "We have begun to dismantle the largest and most effective web of international relationships the world has ever known," Kiesling wrote in a resignation letter to Secretary of State Colin Powell. "Our current course will bring instability and danger, not security."</P>
<P>We have to seize every opportunity to build the struggle against the looming U.S. slaughter in Iraq--and build the kind of movement that can oppose U.S. imperialism in all its forms.</P>


