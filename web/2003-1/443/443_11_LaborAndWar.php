<P><font face="Times New Roman, Times, serif" size="4">Labor leaders discuss response to Bush's attacks</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>AFL-CIO criticizes war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 7, 2003 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>BREAKING WITH more than a half-century of uncritical support for U.S. foreign policy, the AFL-CIO Executive Council passed a resolution sharply criticizing the U.S. war drive against Iraq.</P>
<P>The motion does leave open the possibility of supporting a war backed by the United Nations, stating that "the threat posed by Saddam Hussein deserves multilateral resolve, not unilateral action."</P>
<P>The unanimous decision, made at the federation leadership's annual February meeting, came amid news of a sharp decline in union membership and a vicious attack on unions by Secretary of Labor Elaine Chao.</P>
<P>Chao used the traditional speech by the labor secretary at the event to accuse the unions of corruption and defend the administration's policy of tightening controls on union finances. "In all my years of meeting with secretaries of labor, I've never had one so anti-union," AFL-CIO President John Sweeney told reporters.</P>
<P>When International Association of Machinists (IAM) President Thomas Buffenbarger questioned Chao about the new regulations, an aide handed her a binder. She then read out a series of corruption charges in the IAM. "She had a file on everybody around that table, with their names and their pictures," a labor official present at the meeting told <I>Socialist Worker</I>. "We can only assume by her aggressive response to President Buffenbarger that she would have taken the same approach if others had raised the question. In fact, the cases she read had been uncovered by the Machinists' own internal processes, not the Department of Labor."</P>
<P>Chao's attack shocked even the most conservative union leaders, such as Teamsters President James Hoffa, who have tried to make an alliance with George W. Bush. "She treated us like the way the president is treating world leaders in Europe," the labor official said. "If you don't agree with them, you are the enemy."</P>
<P>The decision to criticize Bush's war drive reflected the fact the labor leaders view the war as a cover for the White House's anti-worker agenda. "People are more clear about seeing through the [White House's] war rhetoric as a way to drive other policy initiatives," such as the ban on unions in parts of the Department of Homeland Security and the attack on social programs, the labor official said.</P>
<P>Along with the attack by Chao came the news of another decline in labor's membership to 13.2 percent of the workforce--and to just 8.5 percent in the private sector. The decline is the result of layoffs in manufacturing and other unionized industries--and could threaten the existence of some unions.</P>
<P>Unfortunately, most top union leaders have continued to negotiate concessionary agreements that allow further job cuts. And rather than develop a comprehensive strategy to deal with these attacks, the AFL-CIO is redoubling its efforts to elect a Democratic President--with a $33 million budget over the next two years. Yet Democratic governors are already implementing severe budget cuts and anti-union policies in states like California, Illinois, Michigan and New Jersey.</P>
<P>Nevertheless, with Chao's attack, the AFL-CIO Executive Council finally may have gotten the message that it's impossible to avoid a head-on confrontation with the Bush administration and the employers who back it.</P>


