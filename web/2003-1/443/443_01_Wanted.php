<P><font face="Times New Roman, Times, serif" size="5"><b>Wanted for conspiracy to commit mass murder</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 7, 2003 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>MORE THAN 10 million Iraqis will be killed, injured, displaced or traumatized by the looming U.S. war, according to the United Nations (UN). That's more than the populations of Los Angeles, Chicago, Detroit, San Francisco, Boston and Denver--combined.</P>
<P>But is George W. Bush slowing down the U.S. war drive? Not for a moment.</P>
<P>Bush claims the war is about disarming Iraq. UN weapons inspectors haven't found any weapons of mass destruction in Iraq. Which only goes to show, according to Washington, that Iraq has succeeded in hiding them.</P>
<P>White House officials claim that intelligence data--especially information from high-level defectors--proves that Iraq has these weapons. But last week, <I>Newsweek</I> magazine exposed the truth after it obtained CIA transcripts of interviews with Hussein Kamel, the highest-ranking Iraqi official to defect from Saddam Hussein's inner circle. Kamel "told CIA and British intelligence officers and UN inspectors in the summer of 1995 that after the Gulf War, Iraq destroyed all its chemical and biological weapons stocks and the missiles to deliver them," the magazine reported.</P>
<P>George Bush and Colin Powell have said the exact opposite about Kamel's testimony. In other words, they lied through their teeth.</P>
<P>Bush claimed last week that a U.S. war against Iraq marked a clash between the forces of civilization and barbarism. "We meet here during a crucial period in the history of our nation, and of the civilized world," Bush declared in a speech to the American Enterprise Institute. "Part of that history was written by others. The rest will be written by us."</P>
<P>Written in blood, that is. The U.S. plans to hit Baghdad with 600 to 800 cruise missiles in the first 48 hours of war--twice the number that were used during the entire 1991 Gulf War. "There will be a very high premium on a very quick resolution, even if it gets a bit bloody," one anonymous Pentagon official told the <I>Washington Post.</P>
</I><P>This war is nothing short of premeditated mass murder. For all of those shocked by Bush's barbarism, it's urgent to make your opposition known. Activists are organizing protests across the country for the national day of action on March 5--with more to come in the days and weeks that follow. Find out about these events--and let others know.</P>
<P>Now is the time to take a stand--and join the movement against Bush's war on Iraq.</P>


