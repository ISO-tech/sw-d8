<P><font face="Times New Roman, Times, serif" size="4">Comic strip takes on the "war on terrorism"</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Bush gets his war on</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Petrino DiLeo</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 7, 2003 | Page 9</font></P>

<P><font face="Arial, Helvetica, sans-serif" size="2"><B>BOOKS: <I>Get Your War On.</B></I> David Rees, Soft Skull Press, 2002, 100 pages, $20.</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>SHORTLY AFTER September 11, Brooklyn resident David Rees began posting his "Get Your War On" comic strips on his Web site. The strip depicts clip-art office workers engaged in often profane, always humorous dialogue about the absurd events that built up as George W. Bush's so-called "war on terror" unfolded. Now a collection of Rees' comics is available in a new book.</P>
<P>Rees excels at highlighting some of the more absurd moments since September 11, while countering the ludicrous rhetoric used to justify the attacks on civil liberties at home and the bombing of civilians abroad.</P>
<P>They aren't subtle, often simply asking the question, "Can you believe this is actually happening?" But the fact that Rees produced and published these strips was comforting to all of us who were having the same thoughts.</P>
<P>The strip's immediate popularity--Rees' page has been accessed millions of times since he launched the strip--was evidence that not everyone was on board for Bush's relentless drive for war.</P>
<P>Some of Rees' best strips also point out the racial and class divisions highlighted by the "war on terrorism" as characters talk about relatives serving in the military after having joined to get an education.</P>
<P>A number of strips also smartly highlight the myth of the supposed "unity" in the U.S. after September 11 as corporate scandals such as Enron began breaking out. "I have a feeling that once I understand everything that happened with Enron, I'm gonna take off my American flag pin!" a character quips.</P>
<P>In another series of strips that Rees pretends to have written in the 1980s, he points out that the U.S. allied itself with both Osama bin Laden and Saddam Hussein, debunking some of the arguments used now to justify the war.</P>
<P>All of his strips can be viewed at www.getyourwaron.com, which is updated semi-regularly. Rees is not collecting one cent from the sale of his book. All proceeds are being donated to the United Nations Association's Adopt-A-Minefield program. But above all, "Get Your War On" is a comic strip for our side. For that reason alone, the book is worth picking up.</P>

<p><font face="Times New Roman, Times, serif" size="4"><b>"Rethinking how I look at the world"</b></font></p>

<font face="Times New Roman, Times, serif" size="3"><P><I>Socialist Worker</I> interviews David Rees, creator of "Get Your War On."</P>

<P><B>YOU'VE SAID in interviews that you don't consider yourself a radical. How would you describe your politics?</B> </P>

<P>I DON'T know. Left of center, I guess. Since September 11, I've done a lot of thinking about how I view the world. I like to take things issue by issue. I'm able to think about things much more clearly if I just kind of do my very best to go into each situation and leave my preconceptions at the door. So I try not to be skeptical of someone's articles because I know if someone's on the left or right. But more often agree with a left perspective than right perspective.</P>
<P>What I tried to do was just to cut through the rhetoric and the language games, which in the case of Enduring Freedom [in Afghanistan] was that we were going to bomb the hell of one of the poorest countries on Earth.</P>

<B><P>WHAT HAS the response been like to the strip?</B> </P>

<P>BY ABOUT 20-to-1, the response has been positive, especially in the fall of 2001. A lot of time people said it was the first time they had laughed or first time they did not feel isolated since September 11. I felt like I was saying something that hundreds of thousands of people were thinking.</P>

<B><P>WHAT WERE you trying to accomplish when you starting producing the strip?</B> </P>

<P>I'VE TRIED to make up goals as I've gone along.  Once I decided to make a book, it made sense that I would raise money for the de-miners in Afghanistan. I never really wanted to tie myself to this project, but now I'm making a strip for every issue of <I>Rolling Stone </I>magazine.</P>

<B><P>NOW THAT Bush has moved to Afghanistan and set his sights on Iraq, what has your reaction been?</B> </P>

<P>IT'S FUNNY. My wife and I were talking about this shortly after September 11. I was really into the idea of going after Iraq. I knew about Saddam Hussein and his sons and how malevolent they are.</P>
<P>Then I watched Afghanistan, which, even though it didn't turn out quite as bad as I thought, it made my spirits fell. Afghanistan is a mess. And we are not committing money or forces to let people walk around safely. I don't have much faith in Hamid Karzai's ability to create a model new democracy in that region. All of that just makes me so skeptical of what's going to happen in Iraq.</P>
<P>For me, the focus of the strip or its strength was that it is just toying with the language and the rhetoric. I'm not necessarily opposed to removing this dictator. The people of Iraq would be better off with a better leader. But that's not what U.S. motives are.</P>
<P>It's so depressing. I really like America and the promises implicit in the founding documents. That's why it makes me so depressed to just do a little research and see all this stuff and this idea of a "benevolent" American Empire. It just seems so insane. And then you look at the people behind these things! Dick Cheney? Donald Rumsfeld? I'm sure Cheney is not up late at nights trying to figure out how to make the Iraqi people's lives better. Of course not.</P>

