<p><a name="Get"></a><font face="Times New Roman, Times, serif" size="4"><b>Get organized against the war</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>On February 15, I marched down Hollywood Boulevard in an antiwar protest against the U.S.-led war on Iraq. There were tens of thousands of protesters with me, holding banners and signs. Most every ethnic group in Los Angeles was represented that I could see. Among the marchers, there was a strong sense of unity and peace, which I believe derived from having come together to stand up for our consciences.</P>
<P>At the end of the march, there was a soundstage, where roughly 15-20 speakers took the mike. Muslim community leaders told stories of escalated racism, deportations and interrogations by authorities. They said that mosques are monitored as if they are terrorist shelters.</P>
<P>One Los Angeles union leader said labor stands "shoulder to shoulder with the antiwar effort." Actor Martin Sheen gave a fiery speech about how the truth must be recovered from the mire of propaganda and hate so that we can live in its light. Other speakers included political commentators, student groups, Filipino antiwar groups, doctors, teachers, ministers and rabbis.</P>
<P>While I marched, I took some photos and jotted down the most memorable writings on signs. Some read: "Practice preemptive protest," "Bush is a weapon of mass destruction," "Fight poverty, not war," "Smart presidents, not smart bombs," "No blood for oil," "Draft the Bush twins," "Regime change begins at home," "Drop Bush not bombs," "My country goes to war and all I got is this lousy duct tape," "Trabajo, si! Guerra, No!" "Money for schools, not war" and "Somewhere in Texas a village is missing an idiot."</P>
<P>The overwhelming message was, don't get mad or depressed--get organized!</P>
<B><P>Karen Leavitt, </B>Los Angeles</P>

