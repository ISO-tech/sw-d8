<p><a name="Boeing"></a><font face="Times New Roman, Times, serif" size="4"><b>Boeing cut corners for profit</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>New details coming out about the explosion of the space shuttle Columbia paint what is now a familiar picture for most workers: corporate cutbacks and arrogance throwing safety to the wind.</P>
<P>In a shocking report by the <I>Miami Herald,</I> four Boeing engineers say that a corporate "consolidation"--layoffs--cost the company dozens of its experienced engineers. Management even put pressure on the remaining, inexperienced safety team to play down negative results from its own analysis program! Boeing makes millions in profits from its government contracts--all the more reason for bosses to cover up problems.</P>
<P>This is in addition to a spate of leaked internal NASA e-mails that confirm NASA's own scientists were drawing alarming conclusions about Columbia's safety. These scientists were also ignored by the management bureaucrats. The result was the explosion of Columbia during landing, killing seven astronauts and spewing debris--some of it highly toxic--across the southern U.S.</P>
<P>What is badly needed are strong unions that can fight mass layoffs and defend workers who blow the whistle on their employers. It's not just a matter of protecting union members' standard of living, but protecting all of society from the bosses' boundless greed.</P>
<B><P>Shaun Joseph, </B>Providence, R.I.</P>

