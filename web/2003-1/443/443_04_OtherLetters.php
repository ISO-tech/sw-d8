<P><font face="Times New Roman, Times, serif" size="4"><b>Marching in Israel against Bush's war</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>The worldwide demonstrations against the war on February 15 were the most inspiring show of solidarity I have seen. I was disappointed to not be able to be at the massive rally in New York City, but I did go to a march in Tel Aviv, Israel, where a couple thousand Jews and Arabs rallied against the war.</P>
<P>Many of the chants and slogans--like "Bush, Blair and Sharon: Number one axis of evil" and "The struggle for Palestine, the struggle for Iraq are one struggle"--reflected the connection between the oppression of Palestinians with the war on Iraq. And many protesters recognized that a war on Iraq will only give more cover for Ariel Sharon to further his barbaric war against Palestinians.</P>
<P>Other slogans like "Blix to Israel now!" highlighted the double standard of uncritical U.S. support for Israel, the most lethal country in the Middle East, while gearing up to slaughter Iraqis in order to disarm a country with no evidence of "weapons of mass destruction."</P>
<P>A large contingent of Palestinians that live within Israel was lively and militant. And while most Israelis at the rally still held some illusions in Zionism and/or a two-state solution, it was good to see that at least a minority of Israelis were grappling with these questions and taking an antiwar and anti-occupation stand.</P>
<B><P>Hadas Their, </B>New York City</P>

