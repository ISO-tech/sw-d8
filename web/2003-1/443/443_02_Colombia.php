<font face="Times New Roman, Times, serif" size="5"><b>Stoking a dirty war in Colombia</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Tristin Adie</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 7, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WITH WASHINGTON gearing up for a new slaughter in Iraq, the crash of an apparent U.S. spy plane in Colombia last month shows how far the "war on terror" has expanded.</P>
<P>Left-wing rebels took three Americans prisoner and apparently killed another after their plane crashed into a southern Colombian jungle on February 13. Amnesty International and several news organizations identified the captives as likely CIA agents, though U.S. officials claim that they were civilian contractors for U.S. Southern Command, helping to conduct intelligence operations in territory dominated by the Revolutionary Armed Forces of Colombia (FARC), the largest of the country's rebel groups.</P>
<P>The U.S. presence in the area was yet another indication of Washington's involvement in the increasingly bloody civil war between left-wing guerillas and the Colombian military, which has the worst human rights record in the Western Hemisphere.</P>
<P>The U.S. has given about $2 billion in aid--most of it military--to Colombia since 2001. At George W. Bush's request, Congress voted recently to send another $500 million for 2003. This comes on the heels of $1.6 billion funneled to the Colombian military under Bill Clinton's Plan Colombia, which provided helicopters, weaponry, surveillance equipment and American Special Forces training for Colombian soldiers.</P>
<P>The Clinton administration's cover story for this aid was the "war on drugs." But the post-September 11 "war on terror" allowed the Bush administration to scrap any pretense of fighting Colombia's drug industry. White House officials instead claim that Colombian rebels are part of an international terrorist threat--and that Washington needs a freer hand to intervene militarily to defeat them.</P>
<P>Congress agreed last year, lifting existing restrictions on how U.S. aid could be used. Colombian President &Aacute;lvaro Uribe, who was elected last year on promises to get tough with the rebels, has seized on this shift to press for even more money.</P>
<P>Colombian officials tried to use the bombing of an elite social club in Bogota in early February and the FARC's capture of the three Americans in a campaign to cast the guerillas as part of an international terrorist network. Uribe's government claims that the FARC has received assistance from the IRA in Ireland and the ETA in Spain, though it doesn't provide any evidence to back this up.</P>
<P>The FARC says that it will release the three Americans only if the Colombian government agrees to release dozens of jailed rebels and provide a demilitarized zone for a prisoner exchange. Bush responded to this by authorizing the deployment of up to 150 U.S. troops to help in the search for the three Americans.</P>
<P>Of course, it's not the lives of Americans in Colombia that Bush is particularly concerned about. He's more worried about protecting Occidental Petroleum, Coca-Cola and other U.S. companies that make money in Colombia.</P>
<P>We have to oppose Bush's military adventures--whether in the form of a new war on Iraq, or its more quiet intervention in Colombia's dirty war.</P>

