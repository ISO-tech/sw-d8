<P><font face="Times New Roman, Times, serif" size="5"><b>CUNY budget cuts</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Afsaneh Moradian</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 7, 2003 | Page 10</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--About 1,500 people rallied at City Hall last week to protest budget cuts and a tuition increase for City University of New York (CUNY) students.</P>
<P>The proposed cuts are severe. About $82 million will be cut from the budget, tuition assistance will be cut by one-third, and tuition will go up $1,200 in the fall.</P>
<P>Many students came out to protest because CUNY is their only hope of a college education. "I'm working two jobs and going to school," said one student from NYC College of Technology. "If they raise tuition, I'm going to have to drop out."</P>
<P>Many students connected the budget cuts with the war. "They're not just cutting the budget," as one speaker put it. "They're taking my sister, my brother to fight their war. We need to keep protesting."</P>


