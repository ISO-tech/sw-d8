<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Obey the U.S.--or pay the price</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 7, 2003 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE PRACTICE of the conqueror blaming the conquered for the casualties inflicted on them is as old as conquest itself. The (in)famous Spanish Requirement of 1513 is perhaps the most straightforward example.</P>
<P>It was called the "requirement" because royal law required it to be read before hostilities could be undertaken against a native people. In Latin and/or Spanish, witnessed by a notary, the Conquistadors would stand before a people they were about to attack, and read the following:</P>
<P>"We ask&#133;that you acknowledge the Church as the ruler and superior of the whole world&#133;But if you do not do this, and maliciously make delay in it&#133;we shall powerfully enter into your country, and shall make war against you in all ways and manners that we can, and shall subject you to the yoke and obedience of the Church and of their highnesses.</P>
<P>"We shall take you, and your wives, and your children, and shall make slaves of them, and as such shall sell and dispose of them as their highnesses may command; and we shall take away your goods, and shall do you all the mischief and damage that we can, as to vassals who do not obey, and refuse to receive their lord, and resist and contradict him.</P>
<P>"And we protest that the deaths and losses which shall accrue from this are your fault, and not that of their highnesses, or ours, nor of these cavaliers who come with us."</P>
<P>How different is this from the present, where we find the Bush administration warning Iraq not to put its citizens in harm's way when the U.S. military juggernaut rolls in for the kill? "The generals in Iraq must understand clearly there will be consequences for their behavior," Bush smugly warned. "Should they choose, if force is necessary, to behave in a way that endangers the lives of their own citizens, as well as citizens in the neighborhood, there will be a consequence. They will be held to account."</P>
<P>This turns logic on its head. The country that is about to be overwhelmed with cruise missiles and carpet-bombing is made to appear as if its own plans for defense are what may "endanger" Iraqi people, rather than U.S. bombs.</P>
<P>This is the twisted logic that blames the victims of relentless war for their own suffering. So, for example, in 1997, Clinton's Assistant Secretary of State James Rubin said of the United Nations sanctions on Iraq, "If the Special Commission [conducting weapons inspections] can't do its work, I don't see how it can be any one country's fault that sanctions are still on. It can only be Saddam Hussein's fault."</P>
<P>The logic was simple. Saddam Hussein, a U.S. ally in the region, slipped his American leash and refused to acknowledge the U.S. as "the ruler and superior of the whole world." Vanquished in a one-sided slaughter in 1991, Iraq was forced by the U.S. to accept weapons inspectors and was subject to debilitating economic and military sanctions that denied the country the goods and equipment necessary to rebuild the country's destroyed infrastructure, creating skyrocketing childhood mortality rates.</P>
<P>Any attempt by Saddam Hussein to resist these humiliating conditions is seen as the outrageous act of a wily madman, which reinforces the necessity of sanctions, of bombing, and now, of his replacement by a U.S.-controlled puppet. The logic is as follows: You, as the conquered country, must obey the conquerors, and if you don't, and we destroy you, it will be your fault.</P>
<P>We should be clear in this coming war who is being invaded and who is the real aggressor. The question should not be, as it is even for some antiwar activists, can we disarm Iraq without war, but who will hold George W. Bush and the U.S. government to account for the hundreds of thousands of lives they have taken and will take in Iraq for the sake of the "credibility" of U.S. power?</P>


