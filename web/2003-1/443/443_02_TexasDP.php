<P><font face="Times New Roman, Times, serif" size="4">Setback for the Texas execution machine</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>A racist system exposed</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Quent Reese</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 7, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. Supreme Court voted by an 8-1 margin last week to give an African American death row prisoner in Texas a new chance to show that prosecutors stacked his jury with whites. Thomas Miller-El, who was convicted for a 1985 double murder, came within hours of execution last year before the Supreme Court agreed to hear his case.</P>
<P>Miller-El and his lawyers say that prosecutors used every trick in the book to disqualify prospective Black jurors, ultimately using challenges to keep nine out of 10 eligible Blacks from being put on the jury. "Happenstance is unlikely to produce this disparity," the Supreme Court's majority wrote in its opinion.</P>
<P>That's an understatement. Miller-El's attorneys showed how the Dallas County prosecutors' office trained its lawyers in how to get rid of people of color from the jury pool. The racism involved in this case was so obvious that all but one Supreme Court justice agreed with the decision. Only right-wing crackpot Clarence Thomas voted against the ruling.</P>
<P>Yet Miller-El's lawyers had made the same argument at every other level of the Texas and federal court systems--and were rejected at every stop.</P>
<P>The decision comes in the midst of continued public questioning of the capital punishment system and open doubts raised by Justices Ruth Bader Ginsburg and Sandra Day O'Connor. This is a sign that continued pressure is having an impact.</P>
<P>But the fact that the Bush administration last week urged the Supreme Court to put even more limits on federal death row appeals involving claims of bad lawyers shows that we still have a fight on our hands. We have to say, "No more executions!"</P>


