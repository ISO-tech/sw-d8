<P><font face="Times New Roman, Times, serif" size="4">State governments slash budgets</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Thrown to the wolves</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 7, 2003 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>PAUL DEAN and NICOLE COLSON report on the state budget disaster.</P></B>

<P>STATES ARE facing their worst fiscal crisis since the Second World War. But when the nation's governors requested emergency help from the federal government, George W. Bush had a simple message: No.</P>
<P>"You know, we've got an issue with our own budget," Bush told the National Governors Association last month, referring to the federal deficit. "You've got issues with your budgets. We can talk about that."</P>
<P>State governments around the country are imposing a single solution to their budget disasters--cuts, cuts and more cuts. And Bush told state leaders that the federal government couldn't do a thing for them--except, perhaps, kick in for more money for cops and homeland security. The results are already wrecking lives from coast to coast--from cuts in Medicaid to reductions of basic services.</P>
<P>Schools are among the hardest hit. In Oregon, for example, state lawmakers wanted to cover a $244 million budget shortfall by eliminating 24 days in the school year. "[I]t'll probably mean that we won't do one of our major units, maybe in science, and the math units," teacher Colleen Garbette told National Public Radio (NPR). "We're not going to get through all the math. I worry about the children having so many days off, especially the kids that struggle, and the children that don't have support at home."</P>
<P>Karla Wenzel, chairperson of the Portland School Board, told NPR: "It feels like a gun to the head. I mean, we've got to come up with the money. The only way to come up with that substantial amount of money, when 85 percent of our budget is people--is people." In other words: layoffs and cutbacks for Oregon teachers.</P>
<P>Oregon already has one of the highest unemployment rates in the country at 7.5 percent, and many of the jobless have exhausted their extended benefits. The result: Oregon is leading the country in hunger, according to the Oregon Food Bank, with 40 percent of food boxes going to children under 17.</P>
<P>The Oregon Health Plan (OHP), which was meant to care for people who can't afford health care, is falling apart because of the budget cuts. Beginning March 1, approximately 100,000 people on the OHP will have their prescription benefits cut. That means that patients will have to take money from their food budget or rent to pay for medicine.</P>
<P>For people who take several medicines, the cost will become prohibitive. One anti-psychotic drug called Zyprexa, which patients used to get basically for free, will now cost a horrifying $1,122 to fill a single prescription.</P>
<P>As <I>Socialist Worker</I> went to press, Oregon legislators were proposing to borrow $300 million, which could restore some services--though this would take several weeks.</P>
<P>But not everyone will feel the pain. When Portland announced that it might cut a month out of the school year, parents from richer areas began organizing "private schools," paying laid-off teachers to teach their kids. Meanwhile, poor and working-class families were left to wonder how they would find and pay for an extra month of child care for their kids.</P>
<P>Oregon isn't alone. Similar shortfalls are plaguing all the states. But the best "relief" that Bush had to offer governors was his economic "stimulus" plan--a scheme for lining the pockets of the wealthiest of the wealthy with even more tax breaks.</P>
<P>"President Bush did not just stiff the nation's governors this week when they came seeking help in handling their ever-growing state deficits," the <I>New York Times</I> wrote in an editorial. "He added insult to injury by lobbying the governors to support his latest wave of upper-bracket tax-cut proposals. These would cost the states an estimated $64 billion over the next decade beyond what they are suffering right now: total deficits of up to $50 billion this year and $80 billion next. As they plead for help, the president could only prescribe more of the same economic 'cure' that helped to create the problem."</P>
<P>There are solutions to this crisis. How about taxing the rich? Or cutting the Pentagon's nearly $400 billion budget--and putting the money that will be spent on a war on Iraq into jobs, health care and education?</P>
<P>The only way that we'll be able to get our fair share is if we organize to stop these cuts--and the rotten politicians responsible for them.</P>

