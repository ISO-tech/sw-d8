<P><font face="Times New Roman, Times, serif" size="5"><b>Held hostage by the racist injustice system</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 17, 2003 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>The justice system here in Chicago is corrupt and racist. I'm a 17-year-old African American prisoner being held hostage here at Cook County Jail. I'm under 23-hour-a-day lock-up on a bogus charge of murder--a crime I did not commit!</P>
<P>Upon my arrest, I was placed in a near-freezing cell by police, with no blanket, sheet or pillow. Cops, all of them white, repeatedly choked, slapped, punched and tortured me until I falsely admitted to the crime. There is no physical evidence or eyewitnesses linking me to the crime.</P>
<P>I've had the honor of meeting Nathson Fields, a former death row prisoner here on the Special Incarceration (SI) unit. He has inspired me to speak out against this corrupt injustice system. </P>
<P>Despite my young age, the jail officers have provided me with no education programs, where I could get my GED. This is frustrating, as I was a junior in high school when I was arrested.</P>
<P>Conditions here at jail are sick and inhumane. My first day here on the SI unit, I was brutally beaten by two guards for no reason other than having the misfortune of being charged with a crime that I didn't commit.</P>
<P>This racist system is corrupt, and it must be fought. I intend to do this now--and once I am freed.</P>
<B><P>Cornell Milton, </B>Chicago</P>

