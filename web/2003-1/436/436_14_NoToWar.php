<P><font face="Times New Roman, Times, serif" size="5"><b>Say no to Bush's war on Iraq</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 17, 2003 | Page 14</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>SOME 15,000 people turned out in Los Angeles on January 11 in the largest protest there so far against the war. A contingent of 50 members of SEIU Local 660 marched, carrying signs that read "Iraq Never Closed My Health Clinic!"</P>
<P>The demonstration was organized by Not In Our Name, ANSWER and Interfaith Communities for United Justice and Peace. Speakers included Jeff Paterson, the first soldier to publicly refuse to fight in the 1991 Gulf War; actor Martin Sheen; and Rep. Maxine Waters (D-Calif.).</P>
<B><P>Stop the detentions!</P>
</B><P>In cities across the country on January 10, activists protested "special registrations" and detentions of immigrants. In San Francisco, 1,000 protesters crowded onto street corners for more than three hours in front of the Immigration and Naturalization Service (INS) building, chanting "Immigrants are welcome here!"</P>
<P>In New York City, 300 people--many with friends and family who had been detained--protested in the bitter cold. "People who came to this country to escape torture and persecution are being detained and tortured here," said a Pakistani immigrant. "We told our children that this is a country where you'll be free, but we come here and the INS and FBI are coming to our houses and taking us away."</P>
<P>"The same ideology that made people think it's okay to kill Amadou Diallo is the same ideology that makes it okay to detain innocent immigrants," said a speaker from the Malcolm X Grassroots Collective.</P>
<P>In Boston, 75 people came out to picket the INS on January 9, chanting "Stop the camps before they start! Free the detainees!" A local union brought their banner that read, "An injury to one is an injury to all."</P>
<B><P>Other organizing</P>
</B><P>In Chicago, about 2,000 people gathered on January 11 to protest against war in Iraq. Featured speakers included Aaron Patterson, one of three exonerated death row inmates released from prison just the day before.</P>
<P>On January 7 in New York, 60 to 70 people from different groups met as part of a new antiwar coalition called United for Peace and Justice. The group is planning a national demonstration for February 15 in New York under the slogan "The World Says No to War" as part of an international day of action.</P>
<P>In Atlanta, some 200 people attended a student teach-in at Georgia State University. In Washington, D.C., 200 people had to be turned away from a sold-out screening of <I>Hidden Wars of Desert Storm. </I>After the film, the audience discussed the lies of Bush Sr.'s administration in the 1991 Gulf War and what can be done to stop the slaughter his son has planned.</P>
<I><P>Ben Dalbey, Desmond Gardfrey, Dasha Haass, Dafni Ioannou, Meneejeh Moradian, Kate O'Neil and Snehal Shingavi contributed to this report.</P>


