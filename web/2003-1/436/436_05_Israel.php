<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISRAEL/PALESTINE</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Scandals shake Sharon reelection bid</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 17, 2003 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>JUST A few weeks ago, Israeli Prime Minister Ariel Sharon and his right-wing Likud Party looked set to win the upcoming January 28 elections. But Sharon has been engulfed by a string of scandals, and he's now in a fight for his political life.</P>
<P>Last week, Israel's <I>Ha'aretz</I> newspaper released a document showing that Sharon received $1.5 million from a South African millionaire to repay an illegal campaign contribution. Sharon had initially told investigators that he mortgaged his ranch to come up with the money.</P>
<P>This revelation comes on the heels of other charges of sleazy dealings. At the party's convention last month, for example, there were media reports that favored positions on the ballot were for sale to the highest bidder, that prostitutes were shepherded into the convention hotel in large numbers, and that a mob figure was given a post in the party's governing body.</P>
<P>Polls now indicate that Likud will win only 30 seats in Israel's 120-member parliament, down from an expected 40 or more. All this spells trouble for the Palestinians.</P>
<P>"To deflect attention from the police inquiry, [Sharon] could get Israel mixed up in some surprising and dangerous capers," wrote <I>Ha'aretz</I> columnist Yoel Marcus. "Bumping off Arafat with a stray missile, for instance. The Teflon Man [Sharon] has become a wounded animal, liable to pitch Israel straight into the inferno."</P>
<P>Already, Israeli military violence against Palestinian civilians and militants has shot up, taking the lives of nine Palestinians in just one 24-hour period last weekend. And Sharon's administration barred Palestinian officials from traveling to London to participate in talks about reforming the Palestinian Authority (PA)--using the suicide attack that killed 23 people in Tel Aviv in early January as a pretext.</P>
<P>But <I>Ha'aretz</I> reporter Aluf Benn laid out the more important reason for this decision. Israeli officials worried that "Palestinian delegates would travel to the London conference, and bask in praise about the PA's willingness to endorse reform," wrote Benn. "The ball would then be thrown back to Israel's court, and Jerusalem would be under pressure to accept concessions."</P>
<P>Israeli officials are also looking for ways to strike out at the PA without angering Washington, which is quietly pressuring Israel to back off as it tries to woo Arab states to support its war against Iraq. The Israeli establishment is, of course, enthusiastic about war on Iraq. Some see the coming war as ideal cover for carrying out a mass expulsion of Palestinians living in the Occupied Territories.</P>
<P>At the moment, Israel is especially desperate for money from the U.S. Its envoys are in Washington this week to seek an additional $4 billion in funds and $8 billion in loan guarantees to offset the cost of repressing the Intifada and restarting Israel's stagnant economy.</P>
<P>The U.S. already bankrolls Israel to the tune of some $5 billion a year--making possible Israel's dirty colonial war to maintain its 54-year seizure of Palestine. It's time to end U.S. support for Israeli apartheid!</P>

