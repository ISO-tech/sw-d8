<P><font face="Times New Roman, Times, serif" size="4">White House "stimulus" proposal has nothing for millions of jobless</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Left behind by the Bush plan</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 17, 2003 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>LEE SUSTAR reports on the victims of the economic slump left behind by the White House "stimulus" plan.</P>
</B><P>JUST DAYS after proposing "a plan that will help people find work," George W. Bush left 1 million jobless workers without any unemployment benefits--even as the terrible job market gets worse.</P>
<P>Bush bowed to pressure earlier this month and asked Congress to reauthorize extended unemployment benefits for the nearly 1 million people cut off on December 28. Yet just as many jobless people remain ineligible for benefits, even though they haven't found work, according to a study by the Center on Budget and Policy Priorities (CBPP).</P>
<P>Meanwhile, the other scheme floated by Bush last week--his proposed "re-employment accounts"--are months, if not years, away from reality, and could be nothing but a scam to gut unemployment benefits overall.</P>
<P>The CBPP study points out that the reason so many people have exhausted their unemployment benefits is that Congress was much stricter about extending them than in the past. In the recession of the early 1990s, Congress allowed workers 20 additional weeks of unemployment benefits beyond the standard 26-week state-funded benefit package. Today, the jobless in high-unemployment states can get only a 13-week extension. And because of increasingly strict state regulations on unemployment benefits, just 44 percent of the jobless are collecting benefits, down from 75 percent in the mid-1970s.</P>
<P>The White House points to the fact that unemployment rates have remained steady at 6 percent as a sign of economic recovery. But that number would be higher if millions of workers hadn't already abandoned hope of finding a job and simply dropped out of the workforce.</P>
<P>In fact, the need for additional jobless benefits is growing more urgent by the day. One-fifth of all those unemployed have been without work for six months, the highest such number since 1992, and payroll employment dropped by 110,000 in December.</P>
<P>"Few job market downturns have been this protracted," the <I>Wall Street Journal</I> observed. "The 29 straight declines in manufacturing employment is the longest stretch of manufacturing retrenchment in post-World War II history. Overall, in the 22 months since the recession began in March 2001, employers have eliminated 1.75 million jobs. By contrast, 22 months after the 1990 recession began, employment had already started to pick up decisively, and fewer jobs--1.57 million--had been eliminated."</P>
<P>Bush's "economic stimulus" plan would do almost nothing to create jobs. Instead of putting more money into the pockets of hard-pressed workers who would spend it, Bush wants to accelerate tax cuts to the wealthy.</P>
<P>The White House claim is that the rich will invest the money and create more jobs. But since profits are at rock bottom because of a glut in nearly every major industry, even record-low interest rates haven't been able to induce businesses to invest.</P>
<P>The "innovation" in Bush's plan--setting up "re-employment accounts" for jobless workers of up to $3,000--are at best a gimmick. At worst, they represent an attempt to undermine the unemployment insurance system.</P>
<P>The money is designed to be used for training or child care while workers search for jobs. Those who get a job would pocket the rest. But according to Jessica Goldberg of the CBPP, the change would require both congressional approval and changes at the state level--which are months away.</P>
<P>"These accounts are not going to provide the immediate stimulus that [extended] temporary benefits provide," she told <I>Socialist Worker</I>. "And these accounts won't help the 1 million workers who have exhausted all state and federal benefits."</P>
<P>As the <I>Palladium-Item</I> newspaper in Richmond, Ind., put it, "Rather than tampering with program packaging as a way of reducing benefits to the working poor, Bush and the Congress should be looking for ways to help more people make meaningful and substantial improvements in the lives of their families."</P>


