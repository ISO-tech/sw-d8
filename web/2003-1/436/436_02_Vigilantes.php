<P><font face="Times New Roman, Times, serif" size="5"><b>Arizona vigilantes' call to arms</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 17, 2003 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>IMMIGRANT BASHERS have a friend in the <I>Tombstone Tumbleweed,</I> a newspaper based in Cochise County, Ariz. Late last year, under the headline "Enough is Enough: A Public Call to Arms," the paper urged the formation of a militia to stop illegal immigration. </P>
<P>The paper's call comes on the heels of a rash of vigilante murders in Arizona. Since June, the bodies of at least 10 immigrants have been discovered in the desert near Phoenix, Ariz.--most with their hands bound and shot in the back of the head, reportedly by two men wearing camouflage fatigues.</P>
<P>"To have the official newspaper of the county call on people to take up arms is very dangerous, very frightening," said Tucson human rights activist Isabel Garcia. "Law enforcement and public officials should be concerned." </P>
<P>They should be--but don't hold your breath. Cochise County Sheriff Larry Dever is publicly friendly with groups like the American Border Patrol--which sends out night patrols of as many as 50 people armed with semi-automatic weapons to hunt undocumented immigrants. Dever even acknowledged attending and speaking at the group's meetings--but says that his presence should be seen as a "voice of reason" rather than support for their "methodology." </P>


