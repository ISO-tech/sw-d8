<P><font face="Times New Roman, Times, serif" size="4">Legislation for mandatory military service</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Bringing back the draft?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 17, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>REP. CHARLES Rangel (D-N.Y.) is cosponsoring legislation to bring back the draft. The proposal would require all U.S. citizens and permanent residents aged 18 to 26 to complete two years of compulsory service, inside or outside the armed forces.</P>
<P>In a December 31 <I>New York Times</I> op-ed article, Rangel wrote: "I believe that if those calling for war knew that their children were likely to be required to serve--and to be placed in harm's way--there would be more caution and a greater willingness to work with the international community in dealing with Iraq."</P>
<P>Rangel's argument has a ring of truth because of the racist reality of the U.S. armed forces today. Minorities make up more than one-third of the military's 1.3 million troops. During the first Gulf War in 1991, more than 25 percent of U.S. soldiers were African American, about twice their proportion in the population as a whole.</P>
<P>Meanwhile, the well-to-do are few and far between in the armed forces--except among the officers who give the orders. All this is because of what peace activists call the "poverty draft." The Pentagon spends $2 billion a year marketing the military to poor and working people as one of the few available opportunities for a steady job. "They take advantage of an economy that increasingly squeezes out those without a college degree, the gutting of college financial aid, and the collapse of affordable housing," says the Central Committee for Conscientious Objectors.</P>
<P>Still, Rangel's proposal is wrongheaded. First, the wealthy have always been able to buy their way out of military service. During the Vietnam War, the U.S. had a draft, and minorities and the poor still ended up in the lowest--and deadliest--ranks of the armed forces. Meanwhile, Washington hawks like George W. Bush, Dick Cheney and Trent Lott got "exemptions" from service, or found a cushy spot in the National Guard. There's no reason to believe that Congress wouldn't include the same loopholes in legislation for a new draft.</P>
<P>Rangel's proposal is wishful thinking on another level, too. He says that a draft would lead to a more "humane" foreign policy, because the rich and powerful would be less willing to send their own children to war. But Washington's war on Iraq--which is already responsible for well over 1 million deaths--should make it clear that there's no price that some hawks won't pay.</P>
<P>Rangel's proposal makes veiled criticism of the Bush war drive, without challenging the real basis of the war. People who really want to stop the war on Iraq should remember that the revolt of working-class soldiers in Vietnam was a key part of the antiwar movement that stopped that slaughter.</P>
<P>Our best hope for stopping the war on Iraq is to build that kind of mass antiwar movement again--not to throw even more kids into the military to be cannon fodder.</P>


