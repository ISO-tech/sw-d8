<p><a name="fight"></a><font face="Times New Roman, Times, serif" size="4"><b>Why the Democrats won't put up a fight</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Robert DeCicco's letter (<I>SW,</I> January 3) makes the good point that progressives need to join forces in the antiwar movement and that we should use the opportunity to build the left. As revolutionary socialists, we agree. </P>
<P>We also think, however, that the antiwar movement should be built on an anti-imperialist basis and that the connection needs to be made between the war abroad and the attacks on workers here at home. </P>
<P>Everyone should welcome Democrats who criticize Bush's insane drive to war. But the Democrats can hardly be seen as sincere. </P>
<P>DeCicco says that the November election was a "Republican landslide" and that the Democrats "will be powerless" to stop the Republican-controlled government. The reality is that the Republicans actually gained a small number of seats to capture Congress. That's hardly a Republican mandate. </P>
<P>Moreover, the Democrats lost control of Congress because they failed to distinguish themselves from the Republicans. The Democrats are failing to stand up to Bush, not because they lack an "electoral strategy," but because they are a party of big business and war. </P>
<B><P>Mike Estrada, </B>San Francisco</P>

