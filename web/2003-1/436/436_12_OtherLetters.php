<p><a name="car"></a><font face="Times New Roman, Times, serif" size="4"><b>Killed because of the cops' car chase</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Last Friday, I found out that a coworker of mine was dead. I first met Qing almost three years ago at work. Everyone liked working with her--she was smart, shy and a nice person. She was getting ready to move to London in two days.</P>
<P>In downtown Chicago, someone stole a wallet from a restaurant and hopped into a car. Police responded and during the chase, Qing was struck and killed. Newspaper reports say that the chase was either 30 miles per hour or 80 miles per hour, so I don't know what to believe. </P>
<P>There are more police out there on the streets than ever before, and high-speed chases are known to be very dangerous, with innocent people the casualties. </P>
<P>I have no idea if the truth will ever come out--given the history of Chicago police cover-ups and lies. I may never know why she had to die for a damn wallet.</P>
<B><P>Bill Kimmel, </B>London</P>

