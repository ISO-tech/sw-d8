<p><a name="war"></a><font face="Times New Roman, Times, serif" size="4"><b>How the war abroad hits here at home</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>I recently read in the newspaper that almost half of the youth in my hometown of Hayward, Calif., qualify for free or reduced lunch. </P>
<P>Last month's official unemployment figures rose to an even 6 percent across the nation, and 750,000 jobless workers lost their unemployment benefits at the start of the New Year--with tens of thousands to come in the upcoming months.</P>
<P>In spite of all these signs, the Bush administration is pressing ahead with its murderous war on Iraq. It is estimated that the war will cost between $20 billion and $200 billion. A 10-year occupation of Iraq would cost the U.S. economy some $1.9 trillion.</P>
<P>This is a slap in the face to U.S. workers. The government is willing to spend billions to extend the American empire and line the pocketbooks of ExxonMobil and Boeing stockholders, but won't extend benefits to unemployed workers. Clearly, this is a government by the bosses, for the bosses.</P>
<P>Already, hundreds of thousands disgusted by Bush's war have taken to the streets of America in protest. We need to convince even more of our friends, family and coworkers to oppose the war on Iraq, and we need to convince these people that not just this war, but the entire system, is rotten. This new war for oil makes it painfully obvious that the socialist alternative is needed now more than ever.</P>
<B><P>John Green,</B> Davis, Calif.</P>


