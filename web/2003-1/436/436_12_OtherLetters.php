<P><font face="Times New Roman, Times, serif" size="4"><b>We don't need to "rethink" tactics</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Within the antiwar movement, some activists are critical of traditional protests and teach-ins. They argue that these are outdated forms of resistance, and that newer, more current means are necessary--like fasting for peace, anti-corporate knitting circles and building shantytowns.</P>
<P>Take, for example, this statement circulated on an antiwar listserv in the Bay Area: "In order to change the gross unconsciousness existing in our society, we must drop it and replace it with our own consciousness...[T]ake the dreams you have and bring them into reality, even if it is in writing on canvas, through voice, sound, dance and song." </P>
<P>Unfortunately, such arguments often underscore elitist and exclusionary tactics. And the arguments to "rethink protests" forget the power of protests of the past. It was a powerful civil rights movement of boycotts, marches and sit-ins that defeated racist Jim Crow laws in the South. It was a powerful rebellion in New York City in 1969 at Stonewall that gave birth to the modern gay rights movement. </P>
<P>And it was a powerful antiwar movement of mass protests that defeated U.S. imperialism in Vietnam and Southeast Asia. During the Vietnam War, what started as a handful of activists in a few cities--through hard work and lessons learned--turned into a mass movement. </P>
<P>By organizing teach-ins and protests, speaking out, circulating petitions and distributing leaflets, the Vietnam antiwar movement helped turn the tide against the war among the mainstream. Today, let's not forget the tools of the left that we can still use effectively to win broad layers of people from every community to oppose Bush's war drive.</P>
<B><P>Martin Smith, </B>Santa Cruz, Calif.</P>

