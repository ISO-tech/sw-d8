<P><font face="Times New Roman, Times, serif" size="5"><b>Organizing in Maryland to keep moratorium</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By John Coursey</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 17, 2003 | Page 15</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE VERDICT is in--the death penalty system in Maryland is racist. That's the conclusion of a two-year study of the state's death row that was released last week. But Maryland's new governor, Robert Ehrlich couldn't care less. He wants to start up the machinery of death as soon as possible.</P>
<P>The study's results were no surprise to death penalty opponents, who for years have singled out Maryland's death row as one of the most racist in the country. Currently, all 12 of Maryland's death row inmates are accused of killing whites--even though minorities are about 80 percent of murder victims. And eight of the 12 death row prisoners are Black.</P>
<P>As the study concluded, "Blacks who kill whites are two-and-a-half times more likely to be sentenced to death than are whites who kill whites, three-and-a-half times more likely than are Blacks who kill Blacks, and almost 11 times more likely to be sentenced to death than 'other' racial combinations."</P>
<P>These outrageous disparities were enough to convince former Gov. Parris Glendening to institute a moratorium on executions last May. But now the moratorium hangs in balance.</P>
<P>Ehrlich, who campaigned as a moderate Republican and "friend" of minorities, has promised to end the moratorium as soon as he takes office. That could mean scheduling as many as seven executions this year alone--more than in any single year in the history of the state.</P>
<P>Maryland has only executed three people since the death penalty was reinstated in 1976. Such a pace of killing will be a tough sell for Ehrlich, especially to a population weary of a system that is blatantly racist and has sentenced the innocent to death.</P>
<P>Many activists were involved in the struggle to win the moratorium last year, and they don't plan to let Ehrlich have his way without a fight. Busloads of people are planning to make the trip to the new governor's inauguration January 15 for a rally to demand that Ehrlich continue the moratorium.</P>
<P>Opponents of the death penalty have drawn inspiration from last week's victory over capital punishment in Illinois. Having the same impact on Ehrlich won't be easy, but Maryland's anti-death penalty movement has never been in a better position to rise to the challenge.</P>
<P>So the message on January 15 will be loud and clear: "Ehrlich, just face it, death row is racist!"</P>


