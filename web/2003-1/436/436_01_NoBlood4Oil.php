<P><font face="Times New Roman, Times, serif" size="4">We're telling George Bush</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>No more blood for oil</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 17, 2003 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"NO SMOKING gun." Last week's announcement from United Nations (UN) chief weapons inspector Hans Blix couldn't have been much clearer. "We have now been there for some two months and been covering the country in ever wider sweeps, and we haven't found any smoking guns," Blix told reporters. Likewise, Mohamed el-Baradei, head of the International Atomic Energy Agency, dismissed U.S. claims that Iraq was developing nuclear weapons.</P>
<P>Try as they might, the Bush administration hasn't been able to manufacture any evidence of weapons of "mass destruction" against Iraq--yet. But even this won't stop the White House from insisting that the weapons are there--and that there needs to be a war to oust Saddam Hussein.</P>
<P>Secretary of State Colin Powell--the Bush administration's supposed dove--told reporters that the U.S. could still go to war even if inspectors fail to find any evidence of weapons violations. "You don't really have to have a smoking gun," he said.</P>
<P>The truth is that the U.S. war drive against Iraq has nothing to do with "weapons of mass destruction"--hidden or otherwise. It's about making a grab for Iraq's oil--and forcing Washington's will on the world.</P>
<P>That obvious truth is fueling widespread doubts about Bush's looming war on Iraq--and leading more and more people to speak out. Last weekend in Los Angeles, some 15,000 people turned out to protest. And on January 18, many more will travel from around the country to Washington, D.C., and San Francisco for national demonstrations against the war machine.</P>
<P>We're sending a message to Washington, loud and clear. No more blood for oil!</P>


