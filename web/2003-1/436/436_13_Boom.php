<P><font face="Times New Roman, Times, serif" size="5"><b>Tossed on the street by the dot-com boom</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Katherine Gorell</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 17, 2003 | Page 13</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>DOCUMENTARY: <I>Boom: The Sound of Eviction,</B></I> produced and directed by Francine Cavanaugh, A. Mark Liiv and Adams Wood.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>WHEN LUXURY housing was constructed for the saviors of the "new economy," the dot-com elite, affordable housing opportunities fell like trees in deregulated old-growth forests.</P>
<P>During the brief gold rush known as the dot-com bubble, San Francisco's topography changed drastically. The gains for a minority of people--mostly fat-cat real-estate developers--in the late 1990s came at a tremendous human cost for the majority.</P>
<P>Working-class families that had called San Francisco home for decades couldn't afford to stay in their neighborhoods and were forced out. A housing crisis still grips the city's poor and working-class neighborhoods, such as the newly hip Mission District, which was predominantly populated by Latino families. </P>
<P>Everywhere they could, landlords found excuses to evict tenants in good standing to line their pockets with some of the dot-com booty. Now, as <I>Boom</I> illustrates, most of these buildings sit empty. City bureaucrats like Mayor Willie Brown and his developer cronies, are interviewed on-screen, explaining why "economic development" is good for all of the city's residents.</P>
<I><P>Boom </I>shows how enraged residents resisted and, in some cases, prevented evictions. From crashing company-sponsored parties to organizing protests, activists--many of them evicted tenants themselves--demonstrated both creativity and tenacity. In addition, music from Antibalas, Aztlan Underground, ColdCut, Fugazi and Tortoise emphasizes the fighting spirit showcased in <I>Boom.</P>
<P>Boom</I> includes examples of creative protest that will inspire. What is ultimately necessary is an alternative to an upside-down world that rewards those who evict the most vulnerable. Until then, displacement of working-class people at the whims of the market will be a constant reality.</P>
<I><P>For more information, go to http://www.boomthemovie.org/.</P>

