<P><font face="Times New Roman, Times, serif" size="4">Wrecking workers' lives for the sake of profit</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>UPS is the real criminal</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 17, 2003 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>I am a job steward at United Parcel Service (UPS), and I am often called into disciplinary hearings. Last month, I had to witness an employee's termination for theft. This man had already admitted stealing a case of 10 cigar packs, so that wasn't in question. He had done it. But the way he was treated by the manager of our shift was truly repulsive.</P>
<P>He was telling our shift manager how this is his first time living alone, and that all of his family lives in New York, and that if he didn't keep this job he would be facing homelessness in the new year. He knew he had done the wrong thing, said he was sorry and asked if he could please keep his job just until the end of the year.</P>
<P>The look in his eyes and the tone of his voice almost broke my heart. He said he had nothing in his fridge, nothing in his pockets, and he basically poured his soul out. This backbreaking, $8.50-an-hour job was his best hope.</P>
<P>He was pouring his heart out for nothing, though. The shift manager, who nodded distantly the whole time, came back as if he was expecting the whole thing. He actually had the nerve to lecture the employee on morality, telling him "stealing is wrong."</P>
<P>"You should learn from your mistakes," he told the devastated employee. Then, the supervisor said that the employee being fired should have thought about his hundred-something dollars in pay plus a whopping $60 in benefits a week (that's all we get as part-timers) before he stole the cigars.</P>
<P>This from a company that made $2 billion last year. UPS made off like bandits from the contract signed this summer with the Teamsters, and the company is going to keep profiting off our labor. </P>
<P>Try telling the thousands of people who Big Brown chews up and spits out every year that all they have to do is straighten up and fly right, and then everything will be hunky-dory. Ask them who the real criminals are--a guy who steals a few cigars, or a company that wrecked, ruined and killed to get to the top.</P>
<B><P>Jon Wexler, </B>Teamsters Local 728, Atlanta</P>


