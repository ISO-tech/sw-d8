<P><font face="Arial, Helvetica, sans-serif" size="2"><b>NO MORE BLOOD FOR OIL</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Ashcroft's war on our civil liberties</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 17, 2003 | Page 6</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ACCORDING TO John Ashcroft, "freedom requires vigilance and security if it is to flourish." And since September 11, Ashcroft and the rest of the Bush gang have been very "vigilant"--about systematically attacking our rights.</P>
<P>Below are some of the "low-lights" of the government's campaign to shred civil liberties.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<B><P>USA PATRIOT Act<BR>
</B>Immediately after September 11, Ashcroft and the Bush administration pushed for this legislation, which gives the government sweeping new wiretap authority, the ability to detain non-citizens indefinitely without charging them, new powers for government agents to obtain financial and other records without probable cause, and fewer barriers to the use of intelligence agencies in domestic law enforcement. </P>
<P>The law also contains such a broad definition of "domestic terrorism" that the government can target activist groups like Greenpeace as terrorists.</P>

<B><P>Spying on activists<BR>
</B>On May 30, 2002, Ashcroft announced that he had "rewritten" the guidelines that govern domestic surveillance. The FBI now has a green light to send undercover agents or informants to spy on worship services, political demonstrations and other public gatherings without the slightest evidence of wrongdoing.</P>

<B><P>Detention of U.S. citizens<BR>
</B>On June 20, 2001, in a speech from Moscow, Ashcroft announced that Abdullah al-Muhajir (who changed his name from Jose Padilla)--the suspect in a vague "dirty bomb" plot--had been labeled an "enemy combatant," removed from the criminal justice system and placed in a military brig for an indefinite period. </P>
<P>Like hundreds of military detainees still being held at Camp X-Ray in Guant&aacute;namo Bay, Cuba, he can be kept behind bars indefinitely without access to a lawyer and subjected to brutal conditions and repeated interrogations.</P>

<B><P>Operation TIPS<BR>
</B>Last year, the Justice Department announced a new program to recruit millions of Americans to spy for the government. Operation TIPS would have trained truck drivers, utility workers, postal workers, and local cable, gas and electric technicians to report activities that they consider suspicious to a Department of Justice hotline. After widespread criticism, the administration was forced to drop its plans.</P>

<B><P>Total Information Awareness<BR>
</B>When Congress approved the new Department of Homeland Security, one of the measures snuck into the legislation was the creation of the Total Information Awareness (TIA) program. TIA will link up commercial and government databases to give the Feds unprecedented access to financial and other information about U.S. citizens.</P>
<P>Among the other Big Brother provisions of the Department of Homeland Security legislation is language that exempts information voluntarily submitted to the new department from the Freedom of Information Act--essentially eliminating any responsibility to answer public questions.</P>
<P>No one could possibly view this offensive as anything other than a power grab by the federal government law enforcement and spy agencies. But don't go complaining to John Ashcroft. "To those who scare peace-loving people with phantoms of lost liberty," he said at a Senate hearing in December 2001, "my message is this: Your tactics only aid terrorists, for they erode our national unity and diminish our resolve. They give ammunition to America's enemies and pause to America's friends."</P>


