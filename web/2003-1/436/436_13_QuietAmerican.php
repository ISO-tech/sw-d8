<P><font face="Times New Roman, Times, serif" size="4">Hollywood silences <I>The Quiet American</I></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The must-see movie that you can't</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Bill Kimmel</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 17, 2003 | Page 13</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>MOVIES: <I>The Quiet American,</B></I> directed by Phillip Noyce, screenplay by Christopher Hampton and Robert Schenkkan, based on the novel by Graham Greene, starring Michael Caine, Brendan Fraser and Do Thi Hai Yen.</P></font>
<font face="Times New Roman, Times, serif" size="3"><I><P>THE QUIET American</I> is a must-see movie--too bad U.S. audiences will have an impossible time finding a theater that's showing it. The movie is based on a 1955 Graham Greene novel about the early years of U.S. involvement in Vietnam. The film was completed shortly before September 11, when Miramax co-chair Harvey Weinstein pulled the plug on the film, calling it "unpatriotic."</P>
<I><P>The Quiet American</I> only saw the light of day after Michael Caine, who plays the lead in the movie, objected and persuaded Miramax to do a limited run in order to qualify for the Academy Awards. So <I>The Quiet American</I> appeared for two weeks in December in a few cities.</P>
<P>The result is that people in the U.S. are denied an opportunity to see a great movie. It's set in Vietnam in 1952. French power is waning as Communist forces are winning efforts to liberate Vietnam.</P>
<P>Caine plays Thomas Fowler, an English reporter who meets "the quiet American"--Alden Pyle, played by Brendan Fraser. Pyle has a job working with medical teams in Vietnam, but this is cover for his real work--as a CIA agent who is attempting to throw U.S. support behind a new force in Vietnamese politics, led by a notorious Vietnamese general, General Th&eacute;.</P>
<P>Even though Th&eacute; is guilty of butchering civilians, the U.S. is arming him and his forces in order to make sure that the Communists do not gain control. As the movie progresses, Pyle's true role slowly emerges, culminating in Th&eacute;'s forces using American explosives in a terrorist attack in a public square, killing dozens of civilians. The bombing was done to place blame on the Communists, with Pyle taking pictures of dying civilians instead of helping them.</P>
<P>The backdrop to this story is Pyle and Fowler's relationship with a young Vietnamese woman named Phuong, played by Do Thi Hai Yen. Trapped economically, she has attached herself to Fowler in the hopes of some stable future.</P>
<P>When Pyle becomes infatuated with Phuong and attempts to steal her away from Fowler, Pyle's wholesome intentions appear clearly at odds with his willingness to participate in a civilian massacre to ensure U.S. hegemony.</P>
<I><P>The Quiet American</I> has some great acting by Caine and is visually stunning. Much of it was shot on location in Vietnam, and the backdrops of city life and scenes in the country are beautiful. The movie exposes the U.S. role in Vietnam, showing how the CIA has supported despots there for years.</P>
<P>It is no surprise that movie executives are unhappy with the politics of the movie--how dare anyone imply that the U.S. kills civilians as part of its foreign policy when it's launching a "war on terrorism" across the globe.</P>
<P>The book is available at bookstores, so grab a copy and keep your eyes open for showings. Perhaps the U.S. studios' greed will outweigh their patriotism as the Oscar race heats up.</P>


