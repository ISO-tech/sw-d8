<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The Republicans' class war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 17, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH administration didn't wait even a week into the New Year to press its advantage in Washington. George W. Bush and his gang are carrying out a kind of domestic equivalent of the so-called "Bush Doctrine" internationally--taking the offensive on many fronts at once, with the goal of establishing their raw power and intimidating even the threat of opposition.</P>
<P>With Republicans in control in Congress, Bush has come up with a hit list of proposals to please the most fanatical of right wingers. Even the bosses' mouthpiece <I>Business Week</I> magazine had to ask on its latest cover: "Class warfare?"</P>
<P>At the top of the list is a so-called "economic stimulus plan" that has nothing to do with stimulating the economy--and everything to do with abolishing taxes for the wealthiest Americans.</P>
<P>The centerpiece of the proposal slashes taxes on dividends for people who own stock. More than half of the benefits will end up in the pockets of the richest 5 percent of Americans--that is, people who make more than $300,000 a year. Compared to the half a trillion devoted to tax breaks for corporations and the rich, the meager sums for extending unemployment benefits are a joke.</P>
<P>Last month, after Sen. Trent Lott (R-Miss.) got canned for expressing his nostalgia for Southern segregation, Bush was full of talk about his concerns about racism. That was shown to be hot air pretty quickly.</P>
<P>Last week, the White House announced that Bush was appointing three dozen reactionaries to federal judgeships--including Charles Pickering, one of Lott's Mississippi buddies who was rejected by the Democrat-controlled Senate last year because of his hostility to civil rights, including his defense of a racist cross-burner.</P>
<P>As <I>Socialist Worker</I> went to press, the Bush Justice Department was expected to announce that it would support a lawsuit before the U.S. Supreme Court aimed at overturning affirmative action at the University of Michigan--and quite possibly nationwide.</P>
<P>Meanwhile, the Republican anti-abortion bigots are going on the offensive with new restrictions on a woman's right to choose.</P>
<P>Bush and his cronies feel pretty cocky about their chances.  No matter how right wing and unpopular the policies that they propose, they think that Congress will roll over for them. The reason for this confidence is obvious.</P>
<P>Ever since Bush took office, and especially since the September 11 attacks, the White House has gotten a free ride from a Democratic "opposition" that seems afraid of its own shadow. Congressional Democrats are talking tough about Bush's "stimulus" plan, but if the past two years are any guide, enough of them will come over to the Republican side for the White House to get most, if not all, of what it wants.</P>
<P>But the one source of opposition that the Republicans, in their snarling arrogance, will overlook lies outside Washington. The bitterness among ordinary people at a government that hands out money to the rich while slashing programs that benefit working people will only build over the next year. And the attacks on working people are coming from an administration that plans to send thousands of working-class men and women into a catastrophic war in Iraq.</P>
<P>People who are who are bitter today can become active in struggles against Bush's agenda tomorrow. It may take some time for these fights to emerge and develop. But the potential is there for building an alternative--step by step--in all the struggles that take place across society.</P>

