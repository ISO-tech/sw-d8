<font face="Times New Roman, Times, serif" size="4">The "opposition" that Bush backs</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Thugs, crooks and con men</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 17, 2003 | Page 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>ELIZABETH SCHULTE reports on the goons that Washington wants to install in power in Iraq.</P>
</B><P>GEORGE W. BUSH claims that the U.S. government is going to war in Iraq "to liberate, not to conquer." But a quick look at the shadowy cast of characters that the White House want to help carry out "regime change" shows exactly what kind of "liberation" it has in mind.</P>
<P>For four days in mid-December in London, more than 300 representatives of the Iraqi "opposition" met at a conference that the White House and State Department hailed as "the broadest gathering ever convened of free Iraqis opposed to the tyrannical regime in Baghdad."</P>
<P>How broad? Some groups represented at the gathering a call for a return of the Iraqi monarchy that was overthrown in 1958.</P>
<P>Patrick Clawson, an analyst with the right-wing Washington Institute for Near East Policy made the Bush administration's cynical calculations clear when he told a reporter: "I want to create the national story that Iraqis liberated themselves." </P>
<P>The main group that the Bush administration has shoved into the spotlight is the Iraqi National Congress (INC), which poses as an umbrella organization bringing together several smaller groups. The INC is made up of wealthy Iraqi exiles based in London and Washington who are loyal to the U.S. but have few connections inside Iraq.</P>
<P>Its leader, Ahmed Chalabi, is a crook. He fled Iraq in 1958, and had to leave Jordan in 1989, where he faces 32 years in prison for embezzling millions from a bank he once owned. Chalabi was made INC leader in 1992 and continues to be a leading spokesperson for the organization, even though the State Department recently discovered that about half of the $4 million that it had given the group wasn't properly accounted for. A delegate at a New York meeting of the INC last year said of Chalabi, "They say Saddam has 300 suits--well, this guy has 400."</P>
<P>Hard-liners like administration adviser Richard Perle have long supported the INC, despite its total inability to build any support among Iraqis. But establishment figures at the State Department and the CIA aren't so sure.</P>
<P>So last summer, they encouraged the formation of an alternative opposition grouping made up of Kurdish groups, the Iran-based Supreme Council for Islamic Revolution in Iraq and the Iraqi National Accord, which is made up of former military officers and Ba'ath party officials in exile.</P>
<P>Despite these maneuvers, however, the Bush administration awarded the INC with $92 million to train fighters--money that it was originally promised by the Clinton administration as part of the Iraq Liberation Act of 1998.</P>
<P>Earlier this month, Scotland's <I>Sunday Herald</I> reported that some 5,000 Iraqi oppositionists--many said to be exiles affiliated with the INC--arrived at Hungary's Taszar air base for intensive arms training and combat practice.</P>
<P>At the top of Washington's list to lead a new regime in Iraq are the ex-generals that the Bush administration hopes might spark a rebellion inside the military. They make for a frightening list of war criminals.</P>
<P>One is Gen. Nizar al-Khazraji, Saddam Hussein's chief of staff from 1980 to 1991. He was the field commander who led the 48-hour chemical weapons attack in 1988 that poisoned and burned 5,000 Kurdish civilians in the northern town of Halabja. An eyewitness testified last year that he kicked a Kurdish child to death after his forces entered a village during the height of the Iraq's campaign against the Kurds in 1988.</P>
<P>Yet former senior State Department official David Mack says that al-Khazraji has "a good military reputation" and "the right ingredients" as a future leader in Iraq. However, al-Khazraji may be out of the running--since he was arrested by Danish police in November and is set to go on trial for war crimes.</P>
<P>So Brigadier Gen. Najib al-Salhi might get the nod. He commanded an armored division of Iraq's Republican Guard in the Gulf War and helped to crush the uprising against the government regime that followed the war. Al-Salhi defected to the U.S. in 1995 and is head of the CIA-sponsored Iraqi Free Officers Movement, a group of military exiles in the Washington suburbs.</P>
<P>Then there's Wafic al-Samarrai, the former head of Iraqi military intelligence living in London. His preference for regime change in Iraq is a "quick covert operation" by the CIA.</P>
<P>Opposition figures disagree on any number of issues. But a couple things are certain. These thugs and businessmen are bought and sold by the U.S. And whatever "regime change" they bring about will only serve to silence the real opposition in Iraq--the ordinary Iraqis who will bear the brunt of U.S. bombs.</P>


