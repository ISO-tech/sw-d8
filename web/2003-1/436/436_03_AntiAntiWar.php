<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Don't buy the criticisms of the ''anti-antiwar'' crusaders</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Antiwar with no apologies</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 17, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE ANTIWAR movement will have another opportunity to show its strength this weekend at national demonstrations in Washington, D.C., and San Francisco. The January 18 marches will be the latest in a string of activities that have included not only national mobilizations, but teach-ins and forums on countless college campuses, and pickets and protests everywhere across the country.</P>
<P>In most cities and towns, activists report the biggest turnouts for protests in many years, if not decades. The movement against this war has grown more quickly than previous struggles--and not only numerically, but in terms of a mobilization that goes beyond the traditional campus base to reach into unions and local communities.</P>
<P>Naturally, the corporate media aren't interested in those who are willing to stand up against Bush's war. They've devoted more space, in fact, to what <I>Nation</I> columnist Alexander Cockburn has called the "anti-antiwar movement"--critics who question not the war, but the motives and tactics of antiwar activists.</P>
<P>Not surprisingly, ex-left-winger-turned-fanatical-hawk Christopher Hitchens is leading the anti-antiwar crusade. But there are plenty of other critics who claim to still be on the left and oppose the U.S. war machine--but who seem to devote all their energies to attacking antiwar activists.</P>
<P>Take the <I>Nation</I>'s David Corn, who recently appeared on Fox News to divulge that celebrities like Susan Sarandon were dupes for appearing at rallies organized by left-initiated antiwar groups like International ANSWER.</P>
<P>Last month, in an <I>LA Weekly</I> column called "Our Peace Movement--Not Theirs," Marc Cooper mocked a speaker at the October 26 ANSWER protest who raised the cases of political prisoners Mumia Abu-Jamal and Jamil Abdullah Al-Amin.</P>
<P>And Todd Gitlin--the former 1960s student leader who has made a career out of criticizing the left--added his two cents in <I>Mother Jones</I>. "If antiwar sentiment turns out to have any impact on the course of events," wrote Gitlin, "it will probably be despite the organized protests, and not because of them."</P>
<P>Of course, the October 26 protests managed to turn out about 200,000 people in cities around the country--and opposition to the war has continued growing since. But Gitlin is too busy complaining about such supposedly "extreme" slogans as "No sanctions, No bombing" to notice.</P>
<P>How can Gitlin demand that antiwar activists apologize for opposing the slaughter that the Bush administration has in store for Iraq--or the horrific economic embargo that has cost at least 1 million Iraqi lives?</P>
<P>In part, the criticisms are nothing more than red-baiting attacks on groups like ANSWER. This has no place in the antiwar movement. Throughout the last century of U.S. imperialist adventures, socialists have played a leading role in building resistance at every step--and they will do so in the fights ahead.</P>
<P>But there is a wider question involved. The truth is that Gitlin and liberals like him accept the same basic political framework that George Bush does--that the U.S. government can be a force for democracy and freedom.</P>
<P>They may recoil from the slaughter of war--and even oppose the Bush administration's plans for a pre-emptive assault on Iraq. But they are perfectly willing to accept the "kinder, gentler" alternative--even if that means, for example, supporting the continuation of murderous U.S. sanctions.</P>
<P>Gitlin and friends justify their attacks on the grounds that anti-imperialist politics would be off-putting to those with more moderate ideas. To be sure, we need a movement united around opposing war in Iraq in which all points of view are welcome. </P>
<P>But the antiwar movement can't avoid taking up such questions--especially with the Bush Doctrine spelling out Washington's goal of using Iraq as a stepping stone for imposing its will around the globe.</P>
<P>No activist should feel defensive about these shameful attacks on our movement. We need to keep building every expression of protest possible--and engage in the political debates along the way that will make the struggle stronger.</P>


