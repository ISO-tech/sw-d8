<P><font face="Times New Roman, Times, serif" size="5"><b>New York City Social Forum</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sarah Grey</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 17, 2003 | Page 14</font></p></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--"Another New York City is possible!" That was the spirit of the New York City Social Forum held January 11.</P>
<P>Based on the World Social Forum held in Porto Alegre, Brazil, and other social forums held around the world, the third annual New York City Social Forum was a day of workshops, debate and organizing aimed at uniting the left. Hundreds attended workshops held by 50 different left-wing groups on topics ranging from "Theater of the Oppressed" to "Organizing for Workplace Justice."</P>
<P>Author and professor Seymour Melman and activist Jennifer Roesch of the ISO led a workshop on "Bush and the New Imperialism," which sparked a lively discussion about what it means for the antiwar movement to unite. "I want to defend the idea of dissent and debate within the movement," Roesch said, pointing out that only by arguing about what imperialism is and where it comes from can we effectively oppose it.</P>
<P>Bringing the left together to debate ideas and plan actions is a crucial step in showing the Washington warmongers that we demand an alternative to war and injustice.</P>


