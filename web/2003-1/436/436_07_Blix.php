<P><font face="Arial, Helvetica, sans-serif" size="2"><b>NO MORE BLOOD FOR OIL</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>How UN inspectors became Washington's tools</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 17, 2003 | Page 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>MANY PEOPLE who oppose Bush's war drive against Iraq nevertheless believe that the United Nations (UN) should have the right to send weapons inspectors to snoop around. Supposedly, the inspectors are legitimate because they are "independent" of the U.S. and other UN member states.</P>
<P>But the opposite is true--and the record of the UN's chief weapons inspector Hans Blix shows why. Blix spent much of the 1990s building a tight connection between the UN weapons inspectors and U.S. spy agencies and military labs.</P>
<P>As director general of the UN's International Atomic Energy Agency (IAEA) in the early 1990s, Blix was the first to use CIA data in North Korea. "Despite the misgivings of third-world members of the IAEA board, Blix, a former Swedish foreign minister, established the right to accept intelligence information supplied by the United States and other member states in its investigations," wrote former <I>Washington Post</I> reporter Don Oberdorfer in his 1997 book <I>The Two Koreas</I>.</P>
<P>The U.S. government, then under George Bush Sr., was only too happy to feed data to Blix. "Taking no chances that the IAEA chief would miss something of importance, U.S. officials provided intelligence briefings for Blix and his top aides in September 1991, March 1992, and on May 7, immediately before his departure" to launch inspections at the North Korean nuclear complex at Yongbyon, writes Oberdorfer.</P>
<P>When Blix acquired nuclear samples from Yongbyon, he sent them to Patrick Air Force Base in Florida for analysis. But in 1994, the Clinton administration reached an agreement with North Korea to de-escalate a crisis in relations, the U.S. itself called a halt to aggressive IAEA inspections--the very inspections that are supposed to take place "independently" of the whims of UN member states.</P>
<P>Still, Blix is responsible for effectively rewriting the way that arms inspections took place. Before 1991, writes Oberdorfer, "the IAEA limited itself to checking civilian nuclear facilities and materials that Nonproliferation Treaty signatories reported in voluntary declarations."</P>
<P>But following the Gulf War, when the Bush Sr. and Clinton administrations wanted to apply extra pressure to Iraq and North Korea, Blix invented "special inspections"--mandatory searches backed up by the threat of UN Security Council sanctions.</P>
<P>In this way, the UN inspectorate became an ever-harsher tool in the hands of nuclear-armed Security Council members whenever they want to punish weaker states for trying to acquire equally destructive weapons.</P>

