<P><font face="Times New Roman, Times, serif" size="4">Iraq Peace Team member speaks from Baghdad:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"The threat is a U.S. war"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 28, 2003 | Page 5</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>LISA MARTENS is a member of the Iraq Peace Team, an international group of antiwar activists--initiated by the anti-sanctions organization Voices in the Wilderness--who traveled to Iraq to show their opposition to the U.S. drive to war.</P>
<P>Some 25 members of the Peace Team remained in Baghdad as the U.S. started its war. Lisa talked to <I>Socialist Worker</I> as dusk fell in Baghdad on March 20, half a day after the first missile attack hit the city, right before dawn. As we spoke, the second U.S. attack of the day cut off our interview.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<P>I WAS out walking today, and people greeted me and were gracious to me in the same way as they usually are. The streets are almost empty, but there were still people out. </P>
<P>People have been living under sanctions and war for well over a decade, so I know that dropping thousands of tons of explosives on them would not be healthy right now. </P>
<P>I'm not going to speak for people in Iraq. I can only speak for myself, but I certainly don't have the right, and I don't think anyone else has the right, to impose on other people what the U.S. government calls "liberation." That's insane--like Canada saying that it's going to liberate the United States from&#133;I don't know what. </P>
<P>I don't see how killing people is liberating them. I think that it's been made really clear by the United Nations that there is no strong evidence of the presence of weapons of mass destruction in Iraq. And it's been made very clear that they need more time to continue that investigation process. </P>
<P>Iraq has not declared war on the United States or threatened to bomb them. The big threat here is that sanctions and war have been killing people in Iraq for years now, and the threat now is that they're going to get killed again by the United States. </P>
<P>At 4 o'clock this morning here in Baghdad, that was when the U.S. ultimatum was supposed to be up. We got our team together at that time. At 5:30, the sirens came on, and I heard about an hour-plus of explosions. I understand that this is a very little bit of what's planned. It was quite far away, but the building rattled. </P>
<P>They're bombing right now--can you hear that? I have to go.</P>

