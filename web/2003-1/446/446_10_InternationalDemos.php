<P><font face="Times New Roman, Times, serif" size="5"><b>"It's the time for action"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 28, 2003 | Page 10</font></p>

<font face="Times New Roman, Times, serif" size="3"><I><P>SOCIALIST WORKER</I> prints excerpts from some of the reports of international antiwar activities that we've received.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Britain</b></font><BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Clive Searle, Manchester Socialist Alliance</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>BRITAIN HAS been in a constant state of protest over the last week. Thousands besieged Parliament last Tuesday as it debated Prime Minister Tony Blair's call to war. In the end, almost a third of MPs opposed war. Blair may have won the vote, but he suffered the biggest rebellion within his own party in modern parliamentary history.</P>
<P>Wednesday saw inspirational walkouts by thousands of school students, who poured into the streets of London, Manchester and Edinburgh and many smaller towns. On Thursday, students were joined by workers who came out to lunchtime protests or "took the day off." Some braved management threats of sacking to take strike action. </P>
<P>"The government is attacking Iraq, and the government is attacking the firefighters," said Bob Pounder, secretary of the Manchester firefighters union. "I believe that people can make up their own minds about who's right." Some 20,000 converged on Parliament Square, keeping MPs penned inside the warmongers' parliament. </P>
<P>Saturday was to be the big test. How could the antiwar movement measure up to the 2 million strong protest that swamped London February 15? We needn't have worried. Some 750,000 people poured into the streets of London--and those who couldn't make it there joined local marches. </P>
<P>When Blair addressed the nation, he had to admit to the deep divisions within the country. But he urged us to get behind our troops now that the war has started. The antiwar movement won't fall for that trap. We will keep standing up for justice, for Palestine, for peace. </P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Egypt</b></font> <BR>
<font face="Times New Roman, Times, serif" size="2"><b>By an Egyptian antiwar activist</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ON THE first day of the war, Tahrir Square in the heart of Cairo was occupied for 12 hours. The call went out for a march on the U.S. embassy for 1 p.m. But by 11:30, hundreds of students had stormed out of classes and started marching toward the embassy, overcoming police blockades. They failed to make it to the embassy, which was defended like a fortress, but they stood their ground until others began arriving for the demonstration.</P>
<P>As traffic was halted, it was clear that this was our biggest demonstration by far. One group started marching to the embassy through Kasr-El-ainy Street. As we got close the embassy, there was a standoff with the police for about 15 minutes before they charged with batons and water cannons. People retaliated with stones.</P>
<P>The standoff continued for another 30 minutes, before people marched back to Tahrir. Tahrir was like a liberated territory. Apparently, people heard about the demonstration after they got back from work and joined us. By sunset, there were people all over the square.</P>
<P>Many I've met, young and old, had the same comment, coming from an old song written by Salah Jahin: "El Sharei Lena"--the street is ours." One young woman commented, "I never understood what that meant, now I do."</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Greece</b></font> <BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Antonis Davanellos</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>A GIANT antiwar demonstration flooded Athens March 21, demanding an immediate halt to the war on Iraq. Police say that more than 100,000 demonstrators took part, while organizers give a far larger number.</P>
<P>At the same time, large antiwar protests--the largest since the 1970s--shook 30 cities in Greece. In a brilliant show of solidarity, the demonstrations coincided with a four-hour political strike by the large public- and private-sector trade union federations to demand an end to the war.</P>
<P>The only work performed by transport workers was to carry people to protests, while journalists at the various media outlets worked only on news of the war, especially the international antiwar demonstrations. The Greek Social Forum is pressing for a 24-hour general antiwar strike, and there's growing sentiment for a Europe-wide antiwar strike.</P>
<P>On March 20, around 100,000 high school students paralyzed Athens. Since then, the city's high schools have practically ceased work. In the evenings, we surround the U.S. embassy or the Greek Parliament with all-night vigils. </P>
<P>It is time for action. We are building a strong antiwar movement, because we know that this battle will determine the possibilities for resistance for workers and youth in every corner of the planet.</P>
<P>In this effort, we know the critical importance of the movement that is growing in the U.S. All of us have our eyes turned on you. Keep up the good fight!</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Italy</b></font> <BR>
<font face="Times New Roman, Times, serif" size="2"><b>By Yurii Colombo</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ON MARCH 16, Italian trade unions called for a two-hour general strike against the war. When the war began on March 20, more than 75 percent of workers went on strike, with public-sector workers striking for eight hours.</P>
<P>On March 15, the General Confederation of Italian Workers--Italy's largest union federation--organized a national demonstration in Milan that 750,000 people participated in. Now, several unions are organizing another general strike against the war for April 2.</P>
<P>On the first day of war, a student demonstration in Milan, with the support of public-sector unions, organized more than 50,000 people in the morning, and more than 10,000 in the afternoon.</P>
<P>In Rome, 50,000 people marched; in Turin and Genoa, over 20,000 turned out.</P>
<P>Last week, a militant from the Milan Social Center was killed by a fascist squad. Another comrade was stabbed 20 times. The night that he died, the police attacked his supporters at the hospital with gas fire guns.</P>
<P>On March 22, the Social Center organized a national antifascist-antiwar march. Their appeal was answered by more than 20,000 people.</P>


