<P><font face="Times New Roman, Times, serif" size="4">Doubts among soldiers and relatives</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"Bush took my only son"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 28, 2003 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"PRESIDENT BUSH, you took my only son away from me." Those were the heartbreaking words of Michael Waters-Bey, whose son, Marine Staff Sgt. Kendall Waters-Bey, was among the first American casualties of the war--four Marines who died when their helicopter crashed in Kuwait near the Iraqi border.</P>
<P>"It's all for nothing--that war could have been prevented," Kendall's sister, Michelle Waters, told a <I>Baltimore Sun</I> reporter, tears running down her cheeks. "Now, we're out of a brother. Bush isn't out of a brother. We are."</P>
<P>These comments were a rare glimpse past the wall of patriotism whipped up by politicians and the media. The wall-to-wall TV news coverage of the war on Iraq has been packed with "human interest" reports about the lives of U.S. soldiers, as well as interviews with their family members--who are almost always encouraged to complain about antiwar demonstrators for not "coming together" to "support our troops."</P>
<P>But even the media propaganda machine hasn't silenced the doubts about a war that millions of people in the U.S. believe they have no stake in. This is true of soldiers, as much as their loved ones. And all of the gung-ho hype of the media--with its "embedded" reporters posing in soldiers' clothing for the camera--can't hide this.</P>
<P>Some European newspapers were reporting that six U.S. soldiers, stationed in Germany, had gone AWOL before being shipped to Iraq, and were being sheltered by the country's antiwar movement--and that six U.S. officers were refusing to carry out orders from the Pentagon on political grounds. The U.S. media haven't breathed a word about it.</P>
<P>But the <I>New York Daily News</I>--not exactly a bastion of antiwar radicalism--had to admit in a story on the eve of the war that "[m]any of the U.S. troops poised for battle here would give peace a chance if they had the choice. Doubts about going to war can be heard openly in conversations among the troops gathered in tents at night and in their random talks about their duties with a reporter&#133;[T]o be sure, the kick-butt attitude appears to be dominant. But even those most eager for combat tend to allow that their disagreeing buddies have valid points to make."</P>
<P>If the war goes according to plan, this questioning will remain beneath the surface. But if the invasion runs into trouble, the politicians and the Pentagon brass, who give the orders without taking any of the risks, may find themselves facing an antiwar opposition inside the military.</P>

