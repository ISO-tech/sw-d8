<P><font face="Times New Roman, Times, serif" size="5"><b>When the U.S. failed to act against genocide</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 28, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>The lies and arrogance of the Bush administration really know no bounds. Prior to the start of the war, White House spokesperson Ari Fleischer declared that if the United Nations (UN) failed to provide cover for the United States' war on Iraq, it will have "failed to act once again," in the face of ethnic cleansing, as it had in Kosovo and Rwanda. </P>
<P>What a sick joke! When in 1994, the genocide of Tutsis in Rwanda was taking place, it was the U.S. government that insisted no genocide was occurring. Then-President Bill Clinton made sure that no one used the word "genocide" in order to ensure that no serious UN response would be demanded. </P>
<P>Over half a million Tutsis died while the U.S. sat by, because Rwanda served no strategic aim for U.S. imperialism. Then-Sen. Bob Dole (R-Kan.) declared: "I don't think we have any national interest here. I hope we don't get involved there." </P>
<P>Four years later, when it was in the U.S. interest to do so, the Clinton administration attacked Serbia in the name of Kosovar Albanians&#150;in the process, making the situation worse for both ordinary Albanians and ordinary Serbs, as they bombed not only military targets, but also hospitals, bridges, power plants, schools and the Red Cross. </P>
<P>The truth is that the U.S. government has no problem supporting ethnic cleansing and dictatorships all over the world when it serves their aims; and then crying out "barbarism!" whenever it needs a pretext for war. </P>
<P>They have long supported Israel's horrific and ongoing ethnic cleansing of Palestinians, and they are all too willing to bribe Turkey into support for their war by allowing them to invade Kurdish-run Northern Iraq--though Turkey has been conducting a genocidal war against its own Kurdish population for decades.</P>
<P>It makes me sick to my stomach to think that those who run our society would have us believe that Iraqi lives will be slaughtered for the sake of "democracy" and "liberation."</P>
<B><P>Hadas Thier, </B>New York City</P>


