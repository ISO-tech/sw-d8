<P><font face="Times New Roman, Times, serif" size="4"><b>Democrats share the blame for war</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Iowa Democratic Sen. Tom Harkin recently declared that he was "fooled" into voting for the congressional resolution authorizing war on Iraq, and that if he had it to do over again, he would vote against it. Harkin claims that he thought Bush would seek a peaceful solution in Iraq, not behave "like the cowboy who rode out of Texas, all guns blazing."</P>
<P>Maybe Bush is a lot smarter than most of us give him credit for. He must be in order to have fooled not only Harkin, but Tom Daschle, Dick Gephardt, Hillary Rodham Clinton and the half of congressional Democrats whose votes gave Bush's war resolution the margin of victory.</P>
<P>But the Democrats were happy enough to share credit for the resolution when it was passed last October. Gephardt helped draft the language authorizing military action, and Daschle declared that "it is neither a Democratic resolution nor a Republican resolution."</P>
<P>So it's only fair that Democrats should share the guilt for a U.S. invasion that will kill and maim hundreds of thousands of Iraqi civilians and turn millions into refugees. </P>
<P>Still, I'm glad to see Harkin flip-flop. At least until the war started, Democrats who voted for war on Iraq were backpedaling--which shows how much has changed in the little more than four months since the war resolution was passed. </P>
<P>Harkin and the rest of his party are like weather vanes that indicate which way the political wind is blowing. But ordinary people in the streets are that wind--and we're getting stronger.</P>
<B><P>Doug Anderson, </B>Iowa City, Iowa</P>

