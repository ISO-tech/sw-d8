<p><a name="Where"></a><font face="Times New Roman, Times, serif" size="4"><b>Where "peace" is a dangerous word</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>When the security guards arrested Stephen Downs at the Crossgates Mall, in the Capital Region of New York, he was wearing a T-shirt advocating peace. Now, after a region-wide call to boycott the mall and demonstrations to test the regulation, the mall management has asked town police to drop the charges--and fired the security guard, a nine-year employee, for following mall regulations by calling the police. </P>
<P>Capitalism is crushing freedom of expression. The Downs family knows well about suppression of expression and censorship--from Vietnamese and Iranian refugees that they welcomed into their home upon arrival in the U.S.</P>
<B><P>Helene T. Smith, </B>Burnt Hills, N.Y.</P>



