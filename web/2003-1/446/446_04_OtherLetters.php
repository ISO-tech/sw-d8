<p><a name="Harassed"></a><font face="Times New Roman, Times, serif" size="4"><b>Harassed for speaking out</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>In the November 17 <I>New York Times,</I> I wrote a letter to the editor in which I said that supporters of Israel are "in the position of having to justify a racist system." This quote earned me a phone call from the FBI. </P>
<P>The agent, who works for the Joint Terrorism Task Force of New Haven, left me two voice mails saying that he wanted to "ask me a few questions." As I had done nothing more than exercise my right to express my political opinions, I figured this had to be a "fishing expedition"--in which the FBI was hoping to find out information regarding activists, Muslims or political groups that I may have contact with. </P>
<P>I ignored the calls for three days, but after returning to my dorm room from the library one day, my frantic suitemate informed me that a "police officer" had stopped by to ask me a few questions. Before I could contact a lawyer, the agent called again. </P>
<P>He said that he wished to meet with me and ask me some questions. I declined, in an effort to protect my rights. Afterwards, I reached a local lawyer through the National Lawyers Guild, and he agreed to represent me pro bono. After an unreturned phone call and a letter from my lawyer to the agent, the FBI ceased its investigation. </P>
<P>As the FBI is given increasing powers to spy and intimidate minorities, activists or anyone else who does not "stand united," this incident further highlights the intrusion of Big Brother into our daily lives. </P>
<P>Apparently my desire to have my tax money allocated toward social services, such as education, jobs and health care, rather than Israel's brutal oppression of the Palestinian people or America's ongoing devastation of Iraq, is tantamount to accusations and investigations of "terrorism." </P>
<P>However, as I learned through this experience, the only effective means to counter these injustices is through people standing behind each other against the government's barrage. We must stand in solidarity with all who are victims of government repression. Unite and fight!</P>
<B><P>Sam Bernstein, </B>New Haven, Conn.</P>

