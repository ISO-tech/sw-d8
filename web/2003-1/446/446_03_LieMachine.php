<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Cranking up their lie machine</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 28, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>IT WAS an inevitable part of Washington's war plan. As <I>Socialist Worker</I> went to press, U.S. forces had "discovered" not one, but two suspected chemical weapons factories on their march toward Baghdad.</P>
<P>Instantly, the 24-hour-a-day cable news networks began reporting the discovery of Iraq's "weapons of mass destruction" as an unquestioned fact. But the circumstances--two facilities that just happened to be located in the path of invading U.S. troops, found on the very day that the Pentagon needed to distract attention from blunders that led to the capture of a squad of U.S. soldiers--point to a frame-up.</P>
<P>There will be other "discoveries" in the coming weeks. They should be viewed for what they are--undiluted war propaganda. Propaganda is a weapon every bit as important to U.S. war plans as its arsenal of bombs and missiles.</P>
<P>Until the war began, opinion polls showed a majority of people opposed to an attack under the circumstances that Bush launched it--without United Nations approval. The White House was counting all along on the flood of patriotism that accompanies the beginning of any war, and it has seen public support rise to more than 70 percent.</P>
<P>The main thrust of the administration's case has been that, once a war is launched, everyone must close ranks. As Fox News' chief maniac Bill O'Reilly declared, "Once the war against Saddam Hussein begins, we expect every American to support our military, and if you can't do that, just shut up." </P>
<P>But opponents of this war have to resist any concession to the pressure. If a U.S. war on Iraq was unjust before it began, then it is no different now--no matter how many U.S. soldiers are "in harm's way." Our movement wants U.S. troops out of "harm's way"--by ending the war and withdrawing them from the Middle East--just as we want to stop the murder of Iraqis.</P>
<P>The "support our troops" hype is meant to bully people into thinking that they have no right to protest--at the very moment that protest matters most. The same goes for the belief--widespread even among opponents of the attack--that, now that the war has started, we should hope for a quick U.S. victory, for the sake of both U.S. soldiers and Iraqi civilians.</P>
<P>First, a quick U.S. victory won't be without cost for ordinary Iraqis. The Bush gang is determined to use the full hellish terror of the U.S. military machine as an example to the rest of the world. More importantly, a quick victory would only embolden Washington to go to war again--whether the target is North Korea or Iran or Venezuela.</P>
<P>The politicians will try to use media images of U.S. casualties to whip up support for their war. But these setbacks will also inevitably force the real questions back into the spotlight: Why is the U.S. killing Iraqis, why is it stoking violence, and why is it putting the lives of U.S. troops at risk?</P>
<P>The answer is the same today as it was before the war--to grab Iraq's oil wealth and expand U.S. power in the Middle East and around the globe.</P>
<P>The worldwide antiwar movement continued to send that message in massive protests last week. If support for the war has risen in the two main aggressor countries--the U.S. and Britain--opposition has only grown elsewhere in the world.</P>
<P>We can't let up for even a moment. The outbreak of war isn't the end for antiwar activists, but the beginning--the beginning of a struggle against the U.S. military machine, whether it is stepping up its slaughter in Iraq, taking control of an occupation, or setting its sights on new targets around the world.</P>

