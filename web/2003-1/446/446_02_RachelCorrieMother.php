<p><font face="Times New Roman, Times, serif" size="4">Rachel Corrie's mother speaks out:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"She was passionate about peace and justice"</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 28, 2003 | Page 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3">ON MARCH 16, 23-year old Rachel Corrie, a Palestinian rights activist working with the International Solidarity Movement, stood in front of an Israeli bulldozer in the city of Rafah in Gaza.</P>
<P>For three hours, there had been a standoff, as Rachel and others blocked the bulldozer from demolishing a Palestinian home. Activists at the scene say that the driver of the bulldozer knew that Rachel was in front of him--and deliberately drove toward her.</P>
<P>Initially, he covered her in sand and other heavy debris on her. Then the bulldozer pushed Rachel to the ground and drove over her--then went into reverse to drive over her again. Rachel's arms, legs and skull were fractured. She died in an ambulance on the way to the hospital.</P>
<P>As if to underline Israel's contempt for Palestinians and anyone who supports them, a few days later, soldiers threw stun grenades and tear gas canisters at a group of Palestinian and international activists gathered for a memorial service at the spot where Rachel was crushed.</P>
<P>Joseph Smith, a student from Missouri, said the group had gathered to lay carnations and plant a tree, when Israeli armored personnel carriers moved in. "They started firing tear gas and blowing smoke, then they fired sound grenades," Smith told a reporter. Later, while some of the 100 remained at the spot, Israeli forces drove by in another convoy--including the very bulldozer that had crushed Rachel to death.</P>
<P>Here, Rachel's mother, Cindy, talks to <I>Socialist Worker's </I>GANESH LAL.</P>

<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>IT'S HARD for me to be fair to Rachel's ideals. She taught me so much. She searched very deeply for answers to questions to learn all the different elements of this issue, and she tried to teach me. She was passionate about many things--about peace and freedom--but she was so gentle at the same time.</P>
<P>One of her goals was to bring attention to the reality of the lives of the people in Gaza. She told me that this was one of the best things she did in her life. She loved the people, and their generosity towards her, although they had so little. She talked of the way people took her into their homes, into their families, and she spoke of their dignity, despite their suffering.</P>
<P>Her death reflects the mindlessness of the whole situation. She believed that the occupation of Palestinian lands was indefensible. But Rachel also had a love for all of humanity. The actions of the Israeli Defense Forces are so horrifying, but I still believe that there are many Israelis and American Jews who oppose what Israel is doing to the Palestinians. So many people in the U.S., many of them Jewish, have shown so much support and sympathy towards us in the last few days. </P>
<P>Rachel's message was one of peace. I've tried really hard to keep my focus on the Occupied Territories. But I have to think about the inconsistencies in our supporting UN resolutions calling for Iraq to disarm, while completely ignoring and abandoning all UN resolutions that have called for Israeli withdrawal from the Occupied Territories.</P>
<P>Rachel was opposed to this war on Iraq. She was frightened about what would happen there if the U.S. goes to war. Now that war is underway, and I pray for the people in Iraq and fear for the suffering they will go through.</P>


