<P><font face="Times New Roman, Times, serif" size="5"><b>"This is a defenseless country"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 28, 2003 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>JEREMY SCAHILL is an independent journalist and correspondent for <I>Democracy Now!</I> the Pacifica Radio Network daily news program. Until a few days before the U.S. war began, Scahill had spent most of the last six months in Iraq, reporting for <I>Democracy Now,</I> the <I>Nation</I> magazine and other publications, and the Web site IraqJournal.org.</P>
<P>He talked to <I>Socialist Worker</I> from New York, shortly after the bombs started dropping.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<B><P>WHAT DO you think the impact of a new war will be on ordinary Iraqis?</P>
</B><P>FIRST OF all, we should say that this isn't a war. This is a mammoth power attacking a largely defenseless country. Iraq has been turned into the world's largest firing squad. War is not even an appropriate word for this. </P>
<P>I've been able to talk with several ordinary Iraqis that I've known over the past several years since the bombing began, and different people are expressing different feelings and emotions. Some people are breaking down and crying and saying that they don't know what's going to happen. But other people are very upbeat and saying that we're used to this. </P>
<P>But people are also afraid of the prospect of U.S. troops coming into Baghdad because they think there will be a tremendous amount of bloodshed. </P>
<B><P>DONALD RUMSFELD says that "the liberation of Iraqis is at hand." What do you think most Iraqis would say to this?</P>
</B><P>LIBERATION DOESN'T come from bombs, liberation doesn't come from sanctions, and liberation doesn't come from targeting innocent civilians. Liberation shouldn't be said in the same sentence with most of the discussion about U.S. policy. </P>
<P>What a lot of people have expressed to me is they believe that if the United States had not imposed sanctions on Iraq--if it didn't create a situation where everyone is entirely dependent on the government for their food, for instance--that Saddam Hussein would have gone the way of Nicolae Ceausescu. He essentially would have been killed by Iraqis years ago. </P>
<P>Even if the U.S. comes in and creates the impression of relative calm, we don't know what kind of explosion is going to happen weeks from now and months from now. </P>
<B><P>HAVING BEEN in Iraq, what do you make of the talk that Iraq represents a threat to the United States?</P>
</B><P>GEORGE W. BUSH currently is the greatest threat to the security of Americans. The action that he took in Afghanistan, attacking yet another basically defenseless country, and now attacking Iraq--this is the greatest army in the world taking on the Ukrainian boys' choir. </P>
<P>For 12 years, the United States has humiliated Iraq as a nation, and as a result, it has humiliated Arabs and Muslims around the world. What George Bush is doing now in Iraq, even if they achieve their goals, is going to inflame hatred all around the world, not just of the American government, but of American people. The things that he has done since he has taken office have made us as unsafe as we have ever been in history.</P>
<P>And the idea that Iraq poses a threat to the United States is absolutely ludicrous. Iraq isn't even considered a threat by the overwhelming majority of its neighbors. Saudi Arabia doesn't view Iraq as a threat, Iran doesn't view Iraq as a threat, Turkey doesn't view Iraq as a threat, Syria and Jordan--none of those countries view Iraq as a threat. Only Kuwait says that in the region, and everyone knows well the relationship between the United States and Kuwait. </P>
<P>And I don't believe the American people believe Bush when Bush tells them that Saddam Hussein is a threat to their security.</P>


