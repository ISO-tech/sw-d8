<P><font face="Times New Roman, Times, serif" size="5"><b>Will the U.S. betray the Kurds again?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 28, 2003 | Page 6</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE KURDS are being betrayed by the U.S.--again. While the U.S. is publicly demanding that Turkey not invade Iraqi Kurdistan, Washington has turned a blind eye as Turkish troops beefed up their presence in Iraqi territory, with a reported 70,000 soldiers massed on the border as <I>Socialist Worker</I> went to press.</P>
<P>And the first U.S. target in the region wasn't even the Iraqi military, but Ansar al-Islam, a small Islamist militia that Washington absurdly claims is linked to both al-Qaeda and Saddam Hussein.</P>
<P>Washington has promised Iraq's Kurdish minority greater autonomy in a "liberated" Iraq, as long as their guerrilla fighters submit to U.S. military orders--and stay out of Kirkuk, the oil-rich city under Iraqi government control that Kurds regard as their historic capital.</P>
<P>At the end of the last Gulf War, George Bush Sr. encouraged the Kurds to carry out an uprising, and their forces succeeded in capturing Kirkuk. The Kurds were on the brink of finally establishing the independent state that they were promised after the First World War. But then Washington gave Saddam Hussein the green light to crack down.</P>
<P>In the following years, the Kurds were allowed to set up an autonomous region--later split in two when the rival Kurdish Democratic Party (KDP) and the Patriotic Union of Kurdistan (PUK) clashed militarily.</P>
<P>Last fall, Washington brokered a unity deal between the two parties with the promise that the Kurds would get an autonomous government in a post-war "liberated" Iraq. But at the same time, the U.S. has promised Turkey that the Kurds won't get the kind of self-government that could set an example for Kurds in Turkey.</P>
<P>Kurds in Turkey saw their language outlawed until 1991, and some 30,000 have been killed and 2 million made homeless in the government's genocidal war.</P>
<P>For the new Gulf War, the U.S. originally planned to base 62,000 U.S. troops in Turkey to invade Iraq--until overwhelming opposition led the Kurdish parliament to reject the measure, despite an offer of $30 billion in aid. So in the opening days of the war, the U.S. rushed Special Forces and other troops into Kurdistan by airplane.</P>
<P>According to U.S. Marine Major Gen. Henry Osman, speaking at the headquarters of the Kurdistan Democratic Party in the town of Salahuddin, the U.S. troops are there to "synchronize humanitarian support operations, assist in the deconfliction of humanitarian and military activities, and coordinate relief in northern Iraq." British journalist Patrick Cockburn provided a translation: "In reality, General Osman is here to prevent the Turks fighting the Kurds, and vice versa."</P>
<P>The U.S. aims to use air power to drive the Iraqi military out of both Kirkuk and Mosul to the north, while sending in U.S. commandos to seize the oilfields around Kirkuk. But an Iraqi defeat in Kirkuk would open the way for Kurds driven out of the city to return--and the rival PUK and KDP militias would soon follow. This would almost certainly trigger a Turkish intervention--and that would be a disaster for the puppet government that Washington has waiting in the wings.</P>
<P>The U.S. hopes to play for time until its forces can occupy Kirkuk and Mosul and prevent a Kurdish-Turkish conflict. That, of course, will postpone the day of real Kurdish self-determination--forever, according to Washington's plans.</P>
<P>As a pro-war think tank, the International Crisis Group, put it, "The United States, which has provided a measure of protection to the Kurds during the 1990s, will have a far greater interest in the nature and success of a future government in Baghdad than in accommodating the Kurds."</P>


