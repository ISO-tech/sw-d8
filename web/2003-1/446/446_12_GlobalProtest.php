<P><font face="Times New Roman, Times, serif" size="5"><b>Wave of protests sweeps the globe</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 28, 2003 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AROUND THE world, the U.S. war on Iraq has provoked outrage, protest and resistance. Last Saturday alone, 1 million people took to the streets of Spain, calling for the resignation of Prime Minister Jose Maria Aznar, who supported the U.S. assault.</P>
<P>In central Madrid, Spanish police in riot gear fired rubber bullets at demonstrators, including well-known actors and celebrities. About half a million people demonstrated in the northeastern city of Barcelona, and tens of thousands more attended rallies in smaller cities.</P>
<P>In London, 750,000 people took to the streets in what one speaker pointed out was the largest wartime demonstration in British history.</P>
<P>In Germany, about 25,000 protesters marched through the streets of Leipzig, along the route that demonstrators took when they toppled the former East Germany in 1989. Hamburg police used water cannons to disperse protesters, including middle and high school students, outside the U.S. consulate. In cities across Germany, more than 80,000 schoolchildren protested. "Let's bomb Texas, they've got oil, too," read one banner.</P>
<P>Across the Middle East, too, there was a huge outpouring of protests in solidarity with the Iraqi people. In Yemen, police opened fire on a demonstration of 100,000 outside the U.S. embassy, killing three and wounding dozens of people. Hundreds of police ringing the embassy compound tried to hold off the crowd, the most militant protest since a price-hike rebellion six years ago. </P>
<P>Defying the Israeli state of siege, about 1,000 Palestinians marched in the Rafah refugee camp in Gaza, and 150 people marched in Bethlehem in the West Bank.</P>
<P>Protests also swept through Asia. In Lehore, Pakistan, more than 100,000 demanded an end to the war. In Indonesia, protesters threw eggs and vegetables at the British embassy, targeted U.S. fast-food restaurants and banks, and blocked traffic. </P>
<P>In South Korea, antiwar demonstrations took place on a daily basis even before the U.S. military began its invasion. Now the protests have become more intense, clashing with police on many occasions. "We are very concerned if this kind of action is accepted in international society, next on the list will be North Korea," said Song Young-gil, a South Korean legislator.</P>
<P>Closer to the U.S., 10,000 people came together in Puerto Rico to demonstrate against war, waving Puerto Rican flags and chanting "Peace on earth, no to war."</P>
<P>In S&atilde;o Paulo, Brazil, 10,000 high school and university students demonstrated and marched on the U.S. consulate, chanting angry antiwar slogans directed at George W. Bush. The protest even received support from embassy workers, who appeared in the windows waving handkerchiefs and applauding.</P>
<P>As British member of parliament Alice Mahon put it, Bush and British Prime Minister Tony Blair "have managed to radicalize a whole generation"--in every corner of the globe.</P>


