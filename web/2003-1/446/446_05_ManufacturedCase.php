<P><font face="Times New Roman, Times, serif" size="4">Exposing the truth about U.S. claims</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Manufacturing a case for war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 28, 2003 | Page 5</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>NICOLE COLSON looks behind the myths about Iraq's "weapons of mass destruction."</P>
</B><P>IT DIDN'T matter how much evidence had piled up that the Iraqi government didn't possess "weapons of mass destruction." Throughout the buildup to war, the Bush administration kept claiming that it did--and used this unproven charge to justify its war drive. So it was inevitable that U.S. forces would "discover" alleged chemical weapons factories during their drive toward Baghdad.</P>
<P>As <I>Socialist Worker</I> went to press, there was no way to tell whether the Pentagon would claim that this was the "smoking gun"--much less whether there was any evidence to back it up. But the U.S. has staked its credibility on finding weapons of mass destruction--so we're bound to hear about more such "discoveries."</P>
<P>Should we believe them? There may be weapons of mass destruction in Iraq that were hidden from United Nations (UN) inspections. But there's even more reason to believe that the U.S. will lie if it can't find any proof.</P>
<P>Remember the "aluminum tubes" that the Bush administration long claimed were evidence of Iraq's plans for a nuclear program--no matter how many times the International Atomic Energy Agency dismissed this as a fantasy?</P>
<P>Plus, the much-hyped documents that supposedly proved Iraq had bought large quantities of uranium from the African country of Niger turned out to be a crude forgery--perhaps concocted with the help of the CIA or Britain's MI-6.</P>
<P>Secretary of State Colin Powell's reaction? "If that information is inaccurate, fine." Powell and his boss regularly backed up their claims about Iraq's weapons by citing the 1995 testimony of high-ranking defector Hussein Kamel, Saddam Hussein's son-in-law and the head of Iraq's nuclear, chemical, biological and missile programs for 10 years.</P>
<P>There's only one problem--the story isn't true. In late February, <I>Newsweek</I> magazine reported that Kamel told the CIA, British intelligence officers and UN inspectors in 1995 that Iraq destroyed its chemical and biological weapons after the 1991 Gulf War. "I ordered destruction of all chemical weapons," reads a transcript of Kamel's interrogation. "All weapons--biological, chemical, missile, nuclear were destroyed." </P>
<P>Kamel specifically mentioned anthrax, saying that "nothing remained" of the program. And when asked if Iraq had restarted the manufacturing of VX gas after its war with Iran, Kamel said no, telling investigators that: "We changed the factory into pesticide production. Part of the establishment started to produce medicine&#133;We gave instructions not to produce chemical weapons."</P>
<P>Even if credible evidence of Iraqi weapons of mass destruction is uncovered, this won't change the fact that "liberation" won't come from the barrel of a U.S. gun. As journalist John Pilger wrote: "[W]hen pictures of exhausted Iraqis greeting their 'liberation' are flashed around the world, remember the faces that will be missing in the crowds--not only those of the children bombed and disposed of as 'collateral damage,' but more than a million faces declared expendable by the American-driven and British-backed economic embargo."</P>
<P>Then ask which country really has the weapons of mass destruction.</P>

<p><font face="Times New Roman, Times, serif" size="4"><b>Murderous impact of Washington's bombs</b></font></P>
<P>THE PENTAGON is calling its war Operation Iraqi Freedom. But in private, when the brass plan the assault, they use a different phrase: "Bug Splat."</P>
<P>That's the name of the computer software that calculates the impact of U.S. weapons of mass destruction. And considering the Pentagon's conclusions about its new "workhorse" bomb--the 2,000-pound Mark-84 JDAM (Joint Direct Attack Munition) bomb--the name is apt.</P>
<P>There was nothing but enthusiasm for the JDAM bomb among the corporate media's cheerleaders as they commented on the footage of the attack on Baghdad--and tried to guess which explosion was caused by a cruise missile and which by a JDAM.</P>
<P>Maybe this bunch should have to see the impact up close. "In nanoseconds, it will release a crushing shock wave and shower jagged, white-hot metal fragments at supersonic speed, shredding flesh, crushing cells, rupturing lungs, bursting sinus cavities and ripping away limbs in a maelstrom of destruction," read one recent media report.</P>
<P>According to "Bug Splat" findings about the JDAM:</P>
<P>--Anyone inside roughly a 250-yard area--more than two football fields from edge to edge--is killed.</P>
<P>--Anyone inside a 750-yard area is likely to suffer serious shrapnel damage.</P>
<P>--To be really safe from the effects of fragmentation, a person must be more than 1,000 yards away--about two-thirds of a mile--from where the bomb goes off.</P>

