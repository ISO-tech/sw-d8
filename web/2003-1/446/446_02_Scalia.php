<P><font face="Times New Roman, Times, serif" size="5"><b>Scalia says rights can be limited</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 28, 2003 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>U.S. SUPREME Court Justice Antonin Scalia thinks the government can restrict our rights during war without violating the Constitution. Responding to a question about Attorney General John Ashcroft's witch-hunt at a speech in Cleveland last week, Scalia scoffed, "The Constitution just sets minimums. Most of the rights that you enjoy go way beyond what the Constitution requires."</P>
<P>Although he didn't say exactly which of our rights would be up for grab, Scalia told the crowd that it could expect "the protections will be ratcheted right down to the constitutional minimum. I won't let it go beyond the constitutional minimum."</P>
<P>We can guess, though, that free speech isn't on that list of "constitutional minimums." The next day, Scalia banned the media from attending a ceremony at the City Club of Cleveland, where he was being given an award--ironically enough, for being a champion of free speech.</P>
<P>According to City Club Executive Director James Foster, Scalia was selected for the "Citadel of Free Speech" award because he has "consistently, across the board, had opinions or led the charge in support of free speech."</P>


