<P><font face="Times New Roman, Times, serif" size="5"><b>United uses war to attack its unions</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 28, 2003 | Page 10</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AIRLINE BOSSES are using the war on Iraq and the threat of bankruptcy to push for massive concessions from workers.</P>
<P>United is leading the pack with its request to a bankruptcy judge to rip up its contracts with its labor unions. Once voided, United would seek to impose nearly $2.6 billion in wage and benefit cuts per year. That's three times the temporary wage cuts--set to expire May 1--that United imposed in January.</P>
<P>United CEO Glenn Tilton is also threatening to open up a low-fare subsidiary that would take over 30 percent of United's business and pay its workers substantially less. And Tilton says that United is preparing to reduce its capacity by 10 to 12 percent at any moment--and lay off a similar proportion of workers.</P>
<P>All of these threats are designed to put a gun to workers' heads--and get them to agree to devastating cuts. United claims that cuts are inevitable because international travel is down significantly as a result of the war. But this is a familiar complaint, echoing what United said after September 11.</P>
<P>Even <I>Chicago Tribune</I> airline analyst David Greising isn't buying it. "On this latest 'perish' warning, runaway labor costs aren't United's only vexation," wrote Greising last week. "The fresh bugaboo: that nasty contract it signed with the bankers who provided bankruptcy financing&#133;Too bad United's management never takes a hard look at itself."</P>
<P>But Bank One and the other banks that United owes money to aren't interested in seeing United liquidated. Nevertheless, United would rather exaggerate its situation as a vehicle to impose harsh cuts on its workforce.</P>
<P>And other airlines are taking United's cue. American Airlines is pushing for $1.8 billion in wage and benefit concessions from workers, claiming it must cut costs to avoid bankruptcy. "As United significantly reduces its labor costs, it absolutely puts additional pressure on American from a labor-cost perspective," whined American spokesperson Bruce Hicks. And US Airways is trying to force its pilots to accept cuts in pensions, citing the same "pressures."</P>
<P>The airlines want workers to pay for their crisis.</P>

