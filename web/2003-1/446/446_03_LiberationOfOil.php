<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Seizing oil and killing Iraqis</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>They call this "liberation"?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 28, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THEY PROMISED a bloodless, high-tech war, with U.S. soldiers welcomed as liberators. Instead, George W. Bush and the Pentagon brass have plunged the U.S. into a savage war of conquest--one in which U.S. troops aren't "welcomed," but instead are "often confronted with hatred," the <I>Financial Times</I> reported.</P>
<P>And no wonder. From the "Shock and Awe" blitz of bombs and missiles on Baghdad, to the house-to-house fighting in the supposedly "secured" cities of Basra, Umm Qasr and Nasiriya, U.S. and British forces are killing innocent civilians.</P>
<P>"Going into the war, U.S. planners had hoped American troops would be greeted by joyous civilians in the southern city of Basra," the <I>Wall Street Journal</I> reported. Instead, the city of nearly 2 million has seen urban warfare--and, according to the United Nations, is on the brink of humanitarian disaster because of a cutoff of electricity and water. But U.S. officials say that humanitarian aid is weeks away--and troops have put the priority on protecting nearby oil fields.</P>
<P>"No Iraqi will support what the Americans are doing here," a Nasiriya man named Nawaf told the <I>New York Times</I>. "If they want to go to Baghdad, that's one thing, but now they've come into our cities, and all Iraqis will fight them." Another man, Sayid Yahia, had to secure the release of his son from the Marines, who had arrested him for possession of a rifle. "Is this your liberation?" he demanded of reporters.</P>
<P>The U.S. setbacks quickly led to finger pointing in Washington, where Defense Secretary Donald Rumsfeld was criticized for his arrogant predictions of instant victory. "The Secretary of Defense cut off the flow of Army units, saying this thing would be over in two days," a retired senior general told the Knight-Ridder newspapers.</P>
<P>Rumsfeld tried to deflect criticism by denouncing supposed Iraqi violations of the Geneva Convention on the rules of warfare--for broadcasting "humiliating" interviews with American POWs.</P>
<P>Never mind that the U.S. media has routinely showed the roundup of Iraqi prisoners--who have been forced to wear burlap sacks on their heads in the desert heat and lie face-down in the sand. And forget that Rumsfeld himself denied that Taliban and al-Qaeda prisoners at the U.S. base in Guant&aacute;namo Bay, Cuba, were entitled to Geneva Convention standards.</P>
<P>The White House wants to whip up outrage over U.S. casualties and imprisonment of American soldiers only to boost support for the war. At the same time, they're accusing Iraqi soldiers of faking surrenders to attack U.S. troops and of hiding in cities among "human shields." </P>
<P>That's the Pentagon's way of preparing the public to tolerate the killing of innocent civilians on a massive scale--which is inevitable as U.S. forces try to overcome the unexpected resistance and prepare for an assault on Baghdad by using even more horrific weaponry.</P>
<P>But every intensification of the killing will compel even more Iraqis to fight back. At the same time, U.S. soldiers will come face to face with the fact that they aren't liberators--but conquerors sent to fight for oil company profits.</P>
<P>One sign of this conflict was an apparent "fragging" incident in a 101st Airborne unit, where an African American soldier and recent convert to Islam rolled several grenades into a command tent, killing one officer and wounding 13, shortly before the unit was due to move out, probably without him.</P>
<P>With every bomb that drops, every civilian killed and every oil well captured, the real aims of Bush's war will become that much clearer--and increasing numbers of U.S. soldiers will come to question the war and oppose it.</P>
<P>If Bush has his way, the invasion of Iraq will be the stepping stone for more and more wars, as the U.S. uses the "war on terror" as an excuse to impose its will on the globe. A mass international antiwar movement has emerged to challenge that agenda--and the U.S. barbarism in Iraq has underscored the urgency of building that movement.</P>


