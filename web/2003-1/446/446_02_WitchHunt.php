<P><font face="Times New Roman, Times, serif" size="4">Iraqis to be "interviewed"</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>A new step in the Feds' witch-hunt</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 28, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE FBI is conducting "voluntary" interviews with more than 50,000 Iraqi nationals living in the U.S. in a sick new escalation of the Justice Department's "war on terror" witch-hunt. According to the ACLU, frightened Iraqis--many of whom fled Saddam Hussein's government--have called to ask if they might be imprisoned or deported.</P>
<P>The FBI is reportedly setting up meetings with local Islamic groups across the country to ask for "support" in identifying terrorists and to promise FBI protection against hate crimes. At the same time, however, one FBI official recently told the <I>Washington Post</I> that any Iraqi found to have an immigration violation would be thrown in jail.</P>
<P>"It is ironic that the government is promising to protect Iraqis against hate crimes, which are attacks based on a person's ethnicity, skin color or religion--in other words, the very kind of profiling the government is resorting to," said the ACLU's Dalia Hashad.</P>
<P>Attorney General John Ashcroft signed an order giving FBI agents and U.S. marshals authority to arrest people on immigration violations--a power that will be used to detain any Iraqis suspected of posing a "wartime threat," according to news reports. Ashcroft ordered the expanded powers so that the FBI could detain several dozen Iraqis who have been under surveillance for being "sympathetic" to Saddam Hussein. </P>
<P>Then there's "Operation Liberty Shield"--the Department of Homeland Security program that was announced as the war began. Hyped as a way of protecting the U.S. from potential terrorist attacks, Liberty Shield will increase border patrols and air space restrictions.</P>
<P>But the cornerstone of the operation is the blanket detention of all new immigrants from approximately 30 undisclosed countries while they seek asylum in the U.S. "It's unfortunate that some people truly seeking asylum may be detained," Homeland Security official William Strassberger told the <I>Los Angeles Times,</I> "but I think it's a small price to pay."</P>
<P>Easy for him to say--Strassberger isn't facing confinement for six months or more. </P>

