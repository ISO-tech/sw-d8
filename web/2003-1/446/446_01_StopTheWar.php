<font face="Times New Roman, Times, serif" size="5"><b>Stop this war!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 28, 2003 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THERE'S ONLY one word to describe the fiery hell that the U.S. government unleashed on Baghdad. Terror. Just minutes into the start of "Shock and Awe," the punishing U.S. bombardment, the city looked like something out of a horror movie.</P>
<P>But it was all too real for 5-year-old Doha Suheil. She was the first of 100 people brought to the Al-Mustansaniya College Hospital after the U.S. blitz began. "The cruise missile that exploded close to her home in the Radwaniyeh suburb of Baghdad blasted shrapnel into her tiny legs--they were bound up with gauze--and, far more seriously, into her spine," reported journalist Robert Fisk. "Now she has lost all movement in her left leg."</P>
<P>And this was only one story that slipped out of Baghdad--around the media wall of silence about casualties on a night when the U.S. launched more cruise missiles than during the entire 1991 Gulf War.</P>
<P>Over the days that followed, the Pentagon stepped up its deadly air war. In Basra, Iraq's second-largest city, U.S. bombs took out the city's electricity and water system, leaving more than 1 million people with hardly a drop to drink. And with U.S. forces facing unexpected resistance, the Pentagon's bared-teeth assault will only grow more savage.</P>
<P>But there was little sign of this in the antiseptic TV coverage of the war. Instead, the corporate media mouthed slogans that would have shamed George Orwell's Big Brother. MSNBC announced the first air strike of the war with the words: "Disarming of Iraq has begun."</P>
<P>This is the kind of rhetoric that the Bush administration and its media lapdogs use so shamelessly. The conquest and colonial occupation of a devastated country by the world's only superpower is called the "liberation" of an "oppressed people." A plan to install a U.S. military dictatorship translates to "spreading democracy." Any attempt by the people of the invaded country to put up a defense is denounced as a "war crime."</P>
<P>Civilian casualties inflicted by the U.S. are blamed on the Iraqi government, which is warned not to use its own people as "human shields"--as if that was the reason for casualties in the bombardment of a city that is home to 5 million people.</P>
<P>Pentagon adviser Richard Perle claimed last week that there is "a general recognition that high moral purpose has been achieved here." How dare he!</P>
<P>Millions of people around the world have stood up against the murderers in Washington, who so casually order a war of terror against Iraq in the interest of grabbing oil wealth and empire. This struggle has never been more urgent. Stop this war now!</P>


