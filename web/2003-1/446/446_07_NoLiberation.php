<font face="Times New Roman, Times, serif" size="5"><b>A U.S. occupation isn't "liberation"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 28, 2003 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NO DOUBT the Pentagon wasn't trying to be ironic when it chose "Operation Iraqi Freedom" as the name for the U.S. invasion of Iraq. But like so much of the Bush administration's Orwellian rhetoric, "Iraqi freedom" and the planned U.S. military occupation of Iraq have nothing in common.</P>
<P>Bush and the neo-conservative ideologues who have been the most determined advocates of "regime change" paint a picture of postwar Iraq as model of democracy that will pose a mortal challenge to autocratic regimes in the region. But this is nothing more than a marketing ploy to make the war and its aftermath sound better.</P>
<P>Throughout last fall, leaks from the Pentagon and the State Department laid out much of the plan for post-Saddam Iraq. These leaks established that a U.S. military official--most likely Gen. Tommy Franks--will run the country for at least two years. In other words, the U.S. is planning a military dictatorship.</P>
<P>An Iraqi "consultative counsel" might have some input into decisions. But plans for a constitution, elections and other trappings of liberal democracy are to be left to some unspecified future.</P>
<P>More recent leaks to the press suggest that a new Iraqi government will provide the Bush administration a laboratory to enact its most far-reaching colonial plans for the Middle East. In addition to the U.S. corporations that will receive contracts to rebuild the country and to run its oil industry, other American companies will get to run newly privatized health and education systems.</P>
<P>A pro-U.S. Iraqi government will have to meet a U.S. requirement of recognizing the state of Israel before the U.S. deems that it is sufficiently "liberated" to have self-rule, according to other reports. This requirement makes a mockery of the idea that a U.S. war will "liberate" Iraq. If the country can't determine its own foreign policy, then it's not free.</P>
<P>The Shiite majority and the Kurdish minority of Iraq have long sought autonomy or independence from the Sunni minority that dominates the central government in Baghdad. Yet the U.S. has already announced that post-Saddam Iraq will be a unitary state, and that Kurds will not gain their independence.</P>
<P>In other words, whatever form of government the U.S. cooks up, it will be based on principles that deny the democratic aspirations of the majority of Iraqis from the start.</P>
<P>An occupying power trying to preserve an unpopular status quo will generate opposition. But the Bush administration has downplayed this throughout, preferring to speak of rosy scenarios of democracy installed at the end of U.S. bayonets.</P>
<P>When army chief Gen. Eric Shinseki told Congress that an occupation of Iraq could require as many as 200,000 troops, stuck there for a period of years, administration officials denounced him. Yet a December U.S. Army War College study backed up Shinseki.</P>
<P>Anthony Cordesman, an analyst for the conservative Center for Strategic and International Studies, thinks carefree administration promises of an easy U.S. occupation are wrong. "Iraq cannot be treated as an intellectual playground for political scientists or ideologues, and must not be treated as if its people were a collection of white rats that could be pushed through a democratic maze by a bunch of benevolent U.S. soldiers and [non-governmental] organizations," Cordesman wrote last December.</P>
<P>Criticizing what he characterized as a "U.S. as Liberator Syndrome," Cordesman warned that "we may or may not be perceived as 'liberators.' We may well face a much more hostile population than in Afghanistan. We badly need to consider the Lebanon model: Hero to enemy in less than a year."</P>
<P>Cordesman's scenario assumes that U.S. forces would initially be greeted as "heroes" by ordinary Iraqis. But this is far from likely if the war leaves thousands of ordinary Iraqis dead and ends with Baghdad and other Iraqi cities destroyed.</P>
<P>There's nothing heroic about this war to establish a direct U.S. colony in Iraq. When one looks behind the rhetoric about "democracy" and "liberation," that's the reality of what's to come.</P>

