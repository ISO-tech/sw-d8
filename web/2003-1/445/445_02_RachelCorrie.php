<P><font face="Times New Roman, Times, serif" size="4">Solidarity activist crushed by a bulldozer</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Victim of Israel's terror</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 21, 2003 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>AN ISRAELI soldier demolishing a Palestinian house last weekend murdered Rachel Corrie, an American college student who went to Gaza to defend Palestinian rights. Corrie was crushed under a Caterpillar bulldozer. And after she disappeared under its tread, the driver promptly backed over her a second time.</P>
<P>Corrie--who went to Palestine with the International Solidarity Movement--is the first international protester to have died as part of Israel's vicious war against the Palestinians. But her name is on a long list of Palestinians killed in a similar manner--crushed by bulldozers or collapsing walls as their houses were destroyed.</P>
<P>Those killed in house demolitions died for many reasons. They might have been sleeping when the bulldozers arrived, since many demolitions are carried out under cover of darkness. Or they simply might not have been quick enough in leaving their homes and possessions. </P>
<P>But whatever the reason, their deaths are not accidents. They are premeditated murders, part of the Israeli government's policy of squelching Palestinian resistance in the West Bank and Gaza.</P>
<P>Within hours of Corrie's death, the Israeli military stepped up its assault. In a Gaza refugee camp, Israeli troops fired on Palestinians with tanks and rockets, killing 10 people, including a 4-year-old girl.</P>
<P>"All the children gathered near the kitchen while the tanks were firing outside from all directions," said the girl's aunt. "The girl was standing next to my daughter when she got a bullet in her chest, leaving her in a pool of blood among the scared children."</P>
<P>In a northern Gaza town, troops rounded up 700 Palestinian men for "questioning." Bloodshed, terror and intimidation--these are the tools used by Israel to break the Palestinian will to resist.</P>
<P>As the U.S. war on Iraq heats up, so has Israel's drive to "ethnically cleanse" Palestinians from their homeland. We should remember Rachel Corrie--and continue the struggle in solidarity with Palestinians that she took part in.</P>


