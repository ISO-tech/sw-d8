<P><font face="Times New Roman, Times, serif" size="4">Supreme Court grants stay with 10 minutes to spare</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Saved from Texas' killing machine</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 21, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>DELMA BANKS JR. had 10 minutes left to live. Just as he was about to be strapped to a gurney to receive the injection of toxic chemicals that would kill him, the call came. The U.S. Supreme Court issued a stay of his execution.</P>
<P>Banks would have been the 300th person executed by Texas' assembly line of death--which has accounted for more than a third of all 835 executions in the U.S. since the reinstatement of capital punishment in 1976. Instead, Texas' 300th victim will likely be Keith Bernard Clay--who was set to die on Thursday of this week, as <I>Socialist Worker</I> went to press.</P>
<P>The circumstances surrounding the stay of Banks' execution are shocking for several reasons. The U.S. Supreme Court intervenes in only about 1 percent of death penalty cases. In this case, former FBI Director William Sessions and three former federal judges submitted a brief to the U.S. Supreme Court calling on the justices to examine the flaws in Banks' case.</P>
<P>As Sessions and the others pointed out, the list of "uncured constitutional errors" in the case goes on and on. Banks--who is Black--was convicted by an all-white jury. Prosecutors kept all four Blacks in the jury pool off the jury, and Banks' trial lawyer raised no objections. And new findings revealed that the prosecution's witnesses were paid and threatened into lying on the stand to get a conviction.</P>
<P>"So much in the Texas system of criminal justice is broken that the situation begs for a moratorium on the death penalty to give the state time to fix the problems," wrote the <I>Austin American-Statesman </I>in its strong call for a halt in executions. "Texas is operating a death train that is running off the tracks, and possibly, even likely, killing innocent people. It took a U.S. Supreme Court decision to give Texas' elected leaders the opportunity to right this system."</P>
<P>But none of this is stopping John Ashcroft's gang. The Justice Department is preparing to kill another prisoner from its death row--only the third in the past 30 years. The planned victim? Louis Jones Jr., a veteran of the 1991 Gulf War, who says that he suffers from severe, personality-altering brain damage after exposure to toxins during service.</P>
<P>At the same time, there are signs that abolitionists are continuing to win ground. In Illinois and Maryland, state legislatures are considering legislation to abolish the death penalty in those states. In Maryland, Republican Gov. Robert Ehrlich has vowed to veto abolition legislation if it passes the legislature. In Illinois, the abolition proposal will likely come to the floor of the Democrat-controlled House in early April.</P>
<P>Death penalty opponents are stepping up pressure on lawmakers to vote for the two pieces of legislation. But winning will depend on intensifying the fight in the coming weeks.</P>


