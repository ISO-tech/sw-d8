<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Washington's imperialist war aims are out in the open</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>A U.S. war on the world</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 21, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE BOMBS and missiles will rain down on Baghdad, and the people slaughtered will be Iraqis. But Washington is waging war on the entire world.</P>
<P>It's a war to grab control of Iraqi oil to put U.S. hands on the jugular vein of the world economy. It's a war against the right of nations to determine their own fate--military conquest and occupation packaged as "liberation." And it's a war to tell yesterday's allies in Europe that they're Washington's rivals today.</P>
<P>The U.S. is so isolated--and Washington's imperial war aims are so naked--that George W. Bush's televised speech declaring the 48-hour countdown to war had to resort to Orwellian "war is peace" doublespeak.</P>
<P>"Free nations have a duty to defend our people by uniting against the violent," Bush said--as if launching 800 cruise missiles at Baghdad in 48 hours was an act of humanitarian kindness.</P>
<P>"The Iraqi regime has used diplomacy as a ploy to gain time and advantage," Bush said--after Washington used the time that United Nations (UN) inspectors worked in Iraq to mobilize 250,000 troops.</P>
<P>"In a free Iraq, there will be no more wars of aggression against your neighbors, no more poison factories, no more executions of dissidents, no more torture chambers and rape rooms," Bush continued.</P>
<P>Aggression against neighbors? The U.S. expanded by annexing half of Mexico and has repeatedly intervened militarily in Latin America for the last 100 years. Poison factories? The U.S. refuses to allow inspection of its chemical weapons facilities. Executions? Bush set records for applying the death penalty as governor of Texas. Torture? The U.S. is admittedly torturing prisoners accused of being al-Qaeda members. Rape rooms? The U.S. Air Force protects men who routinely rape women cadets.</P>
<P>As the countdown to a slaughter began, even mainstream commentators had to admit what the U.S. war on Iraq is really all about. "In fact, the debate is not about Saddam anymore," wrote Fareed Zakaria of <I>Newsweek</I> in an article headlined "The Arrogant Empire." "It's about America and its role in the world."</P>
<P>From the abandonment of a UN resolution authorizing the war, to the idiotic France-bashing by right wingers, Washington is sending a message to the U.S. government's old allies during the Cold War against Russia: If you get in our way, we'll make you pay. It's nothing less than an attempt to transform the world political order in place since the end of the Second World War in 1945.</P>
<P>What Washington plans to put in its place reeks of empire--not only the historic domination of smaller and weaker nations by the great powers, but a world in which a single superpower calls the shots. As if to make the point, Washington's war summit at a U.S. military base in the Azores put Bush alongside the imperialist superpowers of centuries past--Spain, Portugal and Britain.</P>
<P>Bush is banking on the Pentagon's "Shock and Awe" blitzkrieg leading to a quick victory--and demoralizing the mass antiwar movement around the world. Yet even if the U.S. is able to sweep through a shattered Iraq, the military occupation that follows will confirm the arguments of the antiwar movement.</P>
<P>To consolidate their control, Washington's military dictators plan to betray the Kurds, buy off elements of the Iraqi military, hand out lucrative contracts to companies like Dick Cheney's Halliburton and abandon millions of civilians to hunger and chaos.</P>
<P>Yet the size of antiwar demonstrations around the world has been unprecedented. Massive opposition will lead to political crises, from Britain to the Middle East--and could see governments fall and industrial action against the war. Whatever the course of events, the movement that opposes a war on Iraq today will have to be prepared to organize against the occupation of that country tomorrow--and more wars to come.</P>
<P>"It takes little imagination to dream up other scenarios that might call for pre-emptive military action," said Thomas Donnelly, a military analyst at the right-wing American Enterprise Institute. That's why the antiwar movement needs to build opposition to the entire U.S. agenda--of using its superpower status to impose its imperial power on the world.</P>


