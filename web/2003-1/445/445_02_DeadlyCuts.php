<P><font face="Times New Roman, Times, serif" size="5"><b>Deadly toll of budget cuts</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 21, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>DOUGLAS SCHMIDT was put into a coma by budget cuts. Earlier this month, Schmidt, a 36-year-old man from Portland, Ore., suffered a massive seizure and slipped into a coma--eight days after he ran out of his anti-seizure medication.</P>
<P>Because of a $2.5 billion state budget shortfall, state lawmakers put the Oregon Health Plan (OHP) on the chopping block. The OHP, which is supposed to provide coverage for low-income people, started by cutting 8,500 from its "medically needy" program on March 1.</P>
<P>That decision may ultimately cost Douglas Schmidt his life. When the program was cut and his medication ran out, he had to go without. His partner told reporters that when Douglas suffered his seizure, he was waiting for an application so that he could get into one of the "interim" prescription plans being offered by the drug company that manufactures his medicine.</P>
<P>Jean Thorne, director of the Oregon Department of Human Services, told the <I>Oregonian</I> that Schmidt's coma is "a tragic circumstance." But "inevitable" would be a better word for it. As Thorne acknowledged, "[A]s we cut back the safety net, we're going to see similar cases."</P>
<P>Now, Schmidt is lying in the hospital--and may never recover. And worse, another 100,000 Oregonians are slated to lose their health care coverage on July 1.</P>
<P>"For what it cost for one day that my son was in the hospital, they could have paid for his medications for his whole life," his mother Sandra Wierzba, told the <I>Oregonian</I>. "I'd just like to know if they do this on purpose to hurt people&#133;These aren't just numbers, they're destroying human beings."</P>


