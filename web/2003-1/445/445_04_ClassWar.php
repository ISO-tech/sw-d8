<P><font face="Times New Roman, Times, serif" size="4">Making workers pay for Connecticut budget crisis</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Barefaced class war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 21, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>Like most states in the country, Connecticut faces a huge budget deficit this year--$650 million. Governor John Rowland recently signed a bill that would compensate by raising taxes and slashing budgets.</P>
<P>Although Connecticut is the richest state in the country, the burden of paying for the deficit falls disproportionately on the poor. New Haven, the fourth-poorest city in the country, will be hit the hardest, with a total loss of $4.4 million in state funds. The tax increase would hit single filers with incomes as low as $22,500 and joint filers with incomes at the $44,000-a-year level. </P>
<P>Meanwhile, Rowland is opposed to a proposed "millionaire tax" that would only affect people making $1 million or more.</P>
<P>The day after the budget bill was passed, there was an outpouring of anger across the state. Dozens of people were arrested after holding a sit-in on the steps of the Capitol. Many people were angry that the Democrats who represented them actually supported the bill. On a local newspaper's Web site, one outraged citizen wrote: "We may as well live in a dictatorship."</P>
<P>Rowland seems to have this in mind. In the past few months, he used the budget cuts to hold a gun to the head of state workers' unions. After laying off 2,800 employees, Rowland had the gall to say that it was the unions' fault--because they wouldn't accept more concessions. In fact, he is now pushing legislation that would allow the state legislature to "open up" union contracts any time they deem necessary in order to force more concessions. </P>
<P>Most people in Connecticut see these budget plans for what they are: barefaced class warfare. As another posting asked, "Will the money mongrels of this state ever know and understand what it is to live with a daily struggle for a life of quality?"</P>
<B><P>Leela Yellesetty,</B> New Haven, Conn.</P>
