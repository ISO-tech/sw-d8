<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Challenging their war propaganda</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 21, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH is launching his war on Iraq in the face of unprecedented opposition--around the world, but also inside the U.S. A recent CNN poll showed that less than half of Americans support a war on Iraq under the circumstances that it will be carried out--without the authorization of the United Nations Security Council. This is a stunning indictment of the White House--and though no one in Washington will admit it, a testament to the antiwar movement's challenge to Bush's mad drive to war.</P>
<P>But don't expect the situation to stay the same. The Bush gang will pull out all the stops to whip up patriotic support. Even before the bombs started dropping, most of the U.S. political establishment--Republican and Democrat alike--had appealed to "all Americans" to "set aside our differences" and unite behind Bush, now that U.S. soldiers are "in harm's way."</P>
<P>Washington's war fever--and the corporate media's all-war-all-the-time cheerleading--will be a pull even on people who have long had doubts about an attack. The antiwar movement needs to be ready to answer back.</P>
<P>There will be enormous pressure on activists, for example, to "support our troops." We shouldn't give in--because when Washington asks us to "support our troops," what they really mean is to support U.S. war aims.</P>
<P>The intensity of these pleas will only grow if something goes wrong for the U.S. With Iraqi society in a shambles and the country's military already devastated, the odds are that U.S. forces will "win" this war without facing a real fight--a repeat of the 1991 Gulf War in which "friendly fire" killed more U.S. soldiers than anything else.</P>
<P>But resistance in Iraqi cities might lead to casualties. And as the CIA warned in a report last year, if pushed to the edge, Saddam Hussein's government might use chemical weapons that were hidden from weapons inspectors. Likewise, if a terrorist attack were to take place in the U.S. during the war, the Bush gang won't hesitate to blame Iraq.</P>
<P>No one can know whether any of this will happen. In fact, there's good reason to believe that Iraq has been disarmed. But if it does, activists can't retreat.</P>
<P>The Bush administration alone bears the responsibility for the violence that a war will unleash--by relentlessly pursuing military action that will serve no purpose other than to expand U.S. power and profits. They are certain to have the blood of hundreds of thousands of Iraqis on their hands. And they deserve the blame for any U.S. victims of this war for oil and empire.</P>
<P>The antiwar movement has both grown and deepened politically in a short period of time. Thus, some protesters who carried signs reading "Peace is patriotic" last fall have concluded that trying to wrap the antiwar movement in the American flag only helps Bush.</P>
<P>At the same time, there are still questions that the movement needs to answer--for example, why the Bush gang's illegitimate war on Iraq shouldn't be counterposed to a legitimate U.S. "war on terrorism," when they are really the same thing. These are the challenges we face in the coming weeks in building an effective and confident struggle against the U.S. war machine and its drive to dominate Iraq and the world.</P>

