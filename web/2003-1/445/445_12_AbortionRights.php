<P><font face="Times New Roman, Times, serif" size="4">New threat to abortion rights</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Right wing on the rampage</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 21, 2003 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH and the Republican Neanderthals in the Senate are declaring war--this time on women's right to abortion.</P>
<P>Last week, the Senate passed legislation banning abortion procedures that the right wingers call "partial-birth" abortion. "Partial birth" is the term that the Christian Right made up to refer to seldom-used late-term procedures used when a woman's or fetus' health is in danger. The anti-choice bigots have used the term to vilify abortion, claiming that during the procedure, the fetus is partially delivered, and then "killed."</P>
<P>Republicans proposed the measure--but they got plenty of support from Democrats. The measure passed the Senate by a 64-to-33 vote--with 16 Democrats joining 48 Republicans.</P>
<P>Bush praised the Senate vote and vowed to sign the bill into law. "Partial-birth abortion is an abhorrent procedure that offends human dignity," said the man who is preparing to order the Pentagon to rain death on the people of Iraq. "Today's action is an important step toward building a culture of life in America."</P>
<P>In fact, the procedure that the bigots are loosely referring to--which is actually called intact dilation and extraction--is used in the second or third trimester, only when the woman's life or health is in danger.</P>
<P>The legislation passed last week would allow the procedure if a woman could die while delivering, but not if her health was compromised. Physicians who perform the procedure would face a fine--and imprisonment for up to two years.</P>
<P>Since the legalization of abortion in the Supreme Court's 1973 <I>Roe v. Wade</I> decision, the anti-choice fanatics have worked to pass legislation that chips away at a women's abortion rights. From a ban on federally funded Medicaid abortions under the Carter administration, to a wide range of state restrictions, including parental consent laws, mandatory waiting periods and bans on specific procedures during the Clinton years, the right has made it more and more difficult for women to gain access to abortion.</P>
<P>And women--especially those who are poor or working-class and young--have paid the price in the plummeting number of physicians who provide abortion procedures. The "partial-birth" ban is even more dangerous.</P>
<P>Because of the vague language of the legislation, which doesn't use any real medical terminology, the ban opens up the possibility that all abortion procedures could be barred. When a similar ban became law in Wisconsin in 1998, women's health clinics suspended all abortions for weeks because physicians feared that they might be imprisoned.</P>
<P>A "partial-birth" ban has long been the goal of the anti-abortion bigots. Over the last eight years, Congress has passed two such bans, but each was met by a presidential veto. But this time around, George W. Bush--who declared his loyal support for the anti-abortion bigots at their rally on the anniversary of <I>Roe v. Wade</I> in January--is at the helm.</P>
<P>Unfortunately, mainstream women's groups like the National Organization for Women (NOW) are ill-prepared to lead a fight to defend abortion rights. Instead of challenging the anti-abortion attack head-on, they've actually taken up some of the right's arguments--for example, asserting that they, too, are committed to decreasing the frequency of abortions. At the same time, groups like NOW have funneled all their resources into electing "pro-choice" Democratic candidates--a ridiculous strategy, considering that 16 Democrats voted for the ban.</P>
<P>A woman should have the right to say what she does with her own body--including ending an unwanted pregnancy. That's why we oppose Congress' attempt to take us back to the days when abortion was illegal--and often deadly. We won't go back!</P>


