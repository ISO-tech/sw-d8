<font face="Times New Roman, Times, serif" size="5"><b>Ashcroft's war on our civil rights</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 21, 2003 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>JOHN ASHCROFT'S arrogance knows no bounds. And for once, even a judge agrees. Last week, federal judge Michael Mukasey upheld an earlier ruling that so-called "dirty-bomb" suspect Abdullah Al-Muhajir (who changed his name from Jose Padilla) should be allowed to meet with a lawyer to legally challenge his detention as an "enemy combatant."</P>
<P>Al-Muhajir has been held in solitary confinement without access to a lawyer in a South Carolina military prison since May of last year, when he was arrested for allegedly plotting to set off a bomb packed with radioactive material in Washington, D.C.</P>
<P>There never was any "dirty bomb." Al-Muhajir had neither access to bomb-making materials, nor the technical knowledge to build one, and there's no evidence that he had U.S. contacts to help him, either. But that hasn't stopped the attorney general from stripping Al-Muhajir of his constitutional right to a trial and a lawyer. </P>
<P>In reality, Al-Muhajir is a test case for Ashcroft and his gang--to see how much they can get away with. "Lest any confusion remain," Mukasey wrote in his 35-page opinion, "this is not a suggestion or a request that Padilla be permitted to consult with counsel, and it is certainly not an invitation to conduct a further 'dialogue' about whether he will be permitted to do so. It is a ruling--a determination--that he will be permitted to do so."</P>
<P>The Justice Department immediately attacked the judge's decision--and said it planned to appeal yet again. As Mukasey was ruling against the Justice Department, another panel of judges handed the Bush administration a victory--by ruling unanimously that detainees at a U.S. military prison in Guant&aacute;namo Bay, Cuba, have no right to challenge their detention in court.</P>
<P>Because the 16 detainees who filed the lawsuit never technically entered U.S. territory, the appeals court ruled that they can't sue in a U.S. court. In essence, the ruling means that the more than 650 detainees at Guant&aacute;namo could spend the rest of their lives in jail--with no rights.</P>
<P>"John Ashcroft has clearly abused his power," said Laura Murphy, director of the ACLU's Washington office. "He is supposed to be the chief enforcer of the Constitution for the executive branch, but he has given lip service to constitutional rights and has systematically eroded free speech rights, privacy rights and due process in the context of fighting the war on terrorism."</P>

