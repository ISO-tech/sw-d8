<P><font face="Times New Roman, Times, serif" size="5"><b>Obituary: Julius Jacobson</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 21, 2003 | Page 4</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LONGTIME SOCIALIST organizer and writer Julius Jacobson died March 8 just short of his 82nd birthday. Jacobson--Julie, as he was known to his friends--personified the best traditions of revolutionary socialist defiance of the status quo and dedication to changing the world.</P>
<P>As a young man, he was virtually the only supporter of the Russian revolutionary Leon Trotsky amid the pro-Stalin Communist Party members in the co-op apartment building where he was raised on Manhattan's Lower East Side. </P>
<P>He threw himself into the Young People's Socialist League (YPSL) in the late 1930s, when the Trotskyists were working inside the Socialist Party--quickly winning a name for himself as fierce and effective debater. But Jacobson, while never as big a "name" as others in the movement, was the kind of comrade who could always make things happen organizationally when it was needed most.</P>
<P>In the late 1950s, he was managing editor of the <I>New International,</I> the theoretical magazine of the Independent Socialist League (ISL), a Trotskyist tendency that viewed Stalin's USSR and its satellites not as "workers' states," but as exploitative bureaucratic dictatorships. While the magazine was formally edited by Max Shachtman, Jacobson ran the show--and his 1952 analysis of McCarthyism remains worth reading in the age of Ashcroft.</P>
<P>When the ISL dissolved into the Socialist Party and Shachtman moved to the right, Jacobson and his wife and longtime political collaborator, Phyllis Jacobson, launched a new journal in 1961--<I>New Politics</I>.</P>
<P>At a time when the left was just emerging from McCarthyism in the wake of the civil rights movement, <I>New Politics</I> provided a forum for both socialists who outlasted the reaction, as well as a new generation of left wingers. Contributors included Sidney Lens, Noam Chomsky, Michael Parenti, Tony Cliff, Harvey Swados, James Petras, Melvin Dubofsky, August Meier, Gabriel Kolko, Howard Zinn, Hal Draper, Stan Weir and many more.</P>
<P>A typical issue would feature debates on the hot issue of the day, such as the civil rights or antiwar movements, along with material that no other journal in the U.S. could conceive of publishing--a feature on socialist thought in Japan, for example, or Karl Marx's debates with the anarchist Mikhail Bakunin.</P>
<I><P>New Politics</I> ceased publication in the late 1970s, but Julius and Phyllis re-launched the journal in the 1980s. It was in those years that this writer, as a young socialist in New York, met Julius and Phyllis. They were delighted by the interest of ISO members in their work, and obligingly hauled out old issues from the 1960s upon request.</P>
<P>But the Jacobson's weren't interested in nostalgia, but in fighting for a socialist alternative. We owe his memory the obligation of continuing that work.</P>


