<p><a name="Not"></a><font face="Times New Roman, Times, serif" size="4"><b>Not all SUV drivers are rich fat cats</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>In response to Paul Dorn's assertion that working-class people don't drive SUVs (<I>SW,</I> February 21), I have to say that anyone who thinks that doesn't spend enough time around working-class people. The cars parked at both the staff and student lots at the community college where I teach include many SUVs. The same is true at the city hospital where I sell <I>SW</I>.</P>
<P>I also feel that being anti-SUV is just the leading edge of an anti-car position that tends to disapprove of all the cars that working-class people drive--older model American cars, pickups, muscle cars--that are just as gas-guzzling as SUVs, and just as vital to getting people to work on time.</P>
<P>Not everybody lives in a place where it is practical or safe to commute by bicycle or public transportation. Many San Francisco workers commute from as far away as Vallejo--a two-hour commute--and if they want to drive an SUV, more power to them.</P>
<B><P>Elizabeth Terzakis,</B> San Francisco</P>
<I><P>P.S. I drive a fuel efficient Saturn that gets great mileage, because it's made out of plastic and will shatter like a wine glass on impact.</i></P>

