<P><font face="Times New Roman, Times, serif" size="4"><b>They're slashing budgets to fund their war</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>The Republican governor of Massachusetts, Mitt Romney, unveiled his state budget plan last Wednesday and declared the cutting of $2 billion in "waste, inefficiency and mismanagement." </P>
<P>What he found was 2,000 state employees that he hopes to lay off; a prescription drug plan for the elderly and disabled he wants to cut; a state hospital for the mentally ill he plans to close; 150,000 people on Medicare who aren't paying "enough" in premiums that he will raise; free health care for nearly all of the 1 million Medicare recipients he plans to attack by imposing co-payments on; and welfare recipients he seeks to force to work for what meager benefits they do get.</P>
<P>Not only does Romney want to gut health care, he's going after education as well. He plans to privatize three state colleges, increase tuition at the rest of the state colleges and universities by 5 to 28 percent--an almost $100 million increase--while restructuring the whole university system to cut costs. In addition, Romney proposed $1.3 billion in fees and fines, all of this for the sake of not raising taxes, especially for the rich. </P>
<P>This is an attack on us! States all across the county are facing budget cuts--as $400 billion is being spent on Bush's "wasteful, inefficient and mismanaged" military build-up. We need money for jobs, education, and health care not for war!</P>
<B><P>Dafni Ioannou,</B> Boston</P>

