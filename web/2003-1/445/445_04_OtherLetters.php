<p><a name="United"></a><font face="Times New Roman, Times, serif" size="4"><b>No "almost-strike" at United Airlines</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>Regarding Lee Sustar's article ("2002: The year of the almost strikes," February 21): Nothing could be further from the truth at United Airlines than to characterize the situation as an "almost strike." I know. I was appointed to our strike committee during contract negotiations last year.</P>
<P>I remember at the time being offended when a coworker said to me that there was a better chance of United Airlines hiring Osama bin Laden as a pilot than us going on strike. But I should have seen the writing on the wall when my local's vice president gave a nudge and a wink and said, if I did a good job, he would get me on the Executive Board--a supposedly elected body.</P>
<P>We were really just a propaganda committee for the Grand Lodge. Our job was to produce flyers. In my naivet&eacute;, I produced agitational ones. Without exception, they ended up in the trash.</P>
<P>The union was very open about its position. The local officers asserted that unlike the pilots and flight attendants, the International Association of Machinists (IAM) would not oppose United's new low-cost carrier because they want jurisdiction over new jobs. This reminded me of something Mother Jones once said: "There are no limits to which powers of privilege will not go to keep the workers in slavery."</P>
<P>The most important thing I've learned is that having an organization is key to building any kind of fightback. The best thing workers can do to defend themselves from the attacks employers are leveling against them is to build union organizations that are democratic. </P>
<P>While some unions may be pressured from the rank and file, the IAM does not fit that category. As it exists now, the IAM is a shell of a union, a rotting corpse--and when it is finally gone, the world will be a better place.</P>
<B><P>Jennifer Biddle,</B> San Francisco</P>

