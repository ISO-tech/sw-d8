<P><font face="Times New Roman, Times, serif" size="5"><b>Chief wrecker of environment quits</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 30, 2003 | Page 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3">CHRISTIE TODD WHITMAN is on her way out the door as the head of the Environmental Protection Agency (EPA). But no one should buy the media line that this is a protest against the right-wing environment wreckers of the Bush White House.</P>
<P>The main reason that Whitman was chosen to head the EPA in the first place is because of her rotten, pro-business record on environmental issues as governor of New Jersey. Whitman eliminated the jobs of hundreds of New Jersey EPA employees and cut the state budget for protecting the environment by 30 percent. She also created an Office of Business Ombudsman to help businesses go around environmental laws. </P>
<P>As journalist Laura Flanders commented, "Her motto was a rhetorical shingle that would hang perfectly outside her door in Washington: 'Open for Business.'" It's no surprise, then, that during her time as head of the EPA, Whitman oversaw the gutting of power plant regulations, the opening of once-protected wetlands to development, relaxation of the 1972 Clean Air Act, and rejection of the Kyoto Treaty on stopping global warming, among other rollbacks.</P>
<P>Whitman displayed all of the same corporate-first beliefs as her boss when it came to environmental protection. In 2001, for example, Whitman remarked that "states should be given more latitude in meeting federal pollution control rules, all regulations should be subjected to a strict cost-benefit analysis, and the EPA should drop its 'command-and-control' attitude toward environmental protection."</P>
<P>And in her resignation letter last week, Whitman once again defended business interests, stating that her "work has been guided by the strong belief that environmental protection and economic prosperity can and must go hand-in-hand." Meaning? Big business should always get the upper hand when it comes to environmental protection laws that might threaten its profits.</P>



