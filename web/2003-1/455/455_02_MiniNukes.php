<P><font face="Times New Roman, Times, serif" size="5"><b>Senate welcomes mini-nukes</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 30, 2003 | Page 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3">THE U.S. Senate last week gave the nuclear fanatics in the Bush administration what they wanted and voted to lift a 10-year ban on the production of small nuclear weapons. The 51-to-43 vote on May 20 gave the go-ahead for research and development of low-yield nuclear weapons of less than five kilotons. These include nukes that can supposedly burrow deep into the earth and explode without great danger to the people above ground.</P>
<P>Supporters of the weapons say that they could be used to destroy biological or chemical weapons buried underground. "In this new world, there could well be reason to have these weapons," said Sen. Jon Kyl (R-Ariz.).</P>
<P>But people who know the weapons are less confident. Eight leading scientists who signed a letter to the Senate organized by the Union of Concerned Scientists warned, "Low-yield earth-penetrating weapons cannot burrow deep enough and do not have a large enough yield to destroy deep underground targets; moreover, the explosion would not be contained for even low-yield earth-penetrating weapons, and would necessarily result in large amounts of radioactive fallout. "If a nuclear weapon was used to attack chemical or biological agents, it is far more likely that this would result in the dissemination of these agents rather than their destruction."</P>
<P>Three days after lifting the small nuke ban, both the House and Senate overwhelmingly approved $400.5 billion in military spending bills. More than $70 billion will go to upgrading and buying weapons generally; $21 million will go to the development of the underground nukes.</P>
<P>It's clearer than ever which government is the greatest danger to the world--with a nuclear arsenal that it's itching to use.</P>



