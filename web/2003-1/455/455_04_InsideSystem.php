<P><font face="Times New Roman, Times, serif" size="4"><b>When diplomats attack</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">UNITED NATIONS members must get cranky when they miss lunch. In early May, thousands of UN employees and diplomats plundered food, liquor and silverware from dining facilities at UN headquarters in New York City after food workers staged a wildcat strike during lunch hour.</P>
<P>As a result of the walkout, UN Secretary General Kofi Annan and Security Council members who were in a private lunch were served a main course, but no dessert or coffee, and service was shut down elsewhere in the complex. That's when a "high-ranking UN official" ordered that cafeterias be opened as "no-pay zones."</P>
<P>When the doors were opened at 1 p.m., "It was chaos, wild, something out of a war scene," said an Aramark executive. "They took everything, even the silverware." A witness from UN security said the cafeteria was "stripped bare."</P>
<P>Another witness said that "chickens, turkeys, souffles, casseroles all went out the door," while a U.S. diplomat, asked how much liquor was taken from a bar, said, "I stopped counting the bottles." Some well-known diplomats were apparently among the takers--as more than $7,000 worth of food and liquor was taken from the main cafeteria alone.<BR>
<I>--Time</I> magazine, May 3, 2003</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Cops' insulting undercover sting</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">COPS IN Kissimmee, Fla., have hit on a disgusting new way to go unnoticed while trying to catch drivers who run red lights: dress up as homeless people. "Operation Vagrant," a sting operation involving the Florida Highway Patrol, Kissimmee police and the Osceola County Sheriff's Office, has so far nabbed 171 drivers--most of whom ran red lights. </P>
<P>Undercover deputies stood along streets and gave the indication that they were vagrants by pushing shopping carts and wearing fake teeth and tattered clothing. "Our ultimate goal is to make an impact," said Osceola Sheriff's Burreau Chief Jerry Geier, "The last thing we're trying to do is disrespect anybody."<BR>
<I>--Orlando Sentinel</I>, May 23, 2003</P>

