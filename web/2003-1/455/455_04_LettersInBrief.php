<p><font face="Arial, Helvetica, sans-serif" size="2">May 30, 2003 | Page 4</font></p>

<P><font face="Times New Roman, Times, serif" size="4"><b><i>SW</i> should be more critical of Cuba's crackdown</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker</B>,</P>
</I><P>In your recent article by H&eacute;ctor Reyes (<I>SW, </I>May 16), he ends the article by stating: "It's up to Cuban workers to wrestle power away from the Castro clique--while continuing to fight to keep Uncle Sam out. That's why we also believe that there is no such thing as the right to do the bidding of U.S. imperialism in the name of dissidence."</P>
<P>Is the author saying that he supports the Cuban government's recent executions of the three hijackers because they had met with or received material support from the U.S. government? Does he support the lengthy prison sentences of the other defendants for their writings and organizational activities because of contact with representatives of the U.S. government?</P>
<P>It has now been established that Solidarity in Poland received material support from the CIA. We did not approve of repression of the Solidarity movement then, based on charges by the Polish government that the U.S. government provided material support. </P>
<P>Aren't the democratic rights of the Cuban people worth supporting? Or of the people in any country? Do we side with the dictatorship simply because the U.S. government is taking advantage of the situation to advance what it perceives as its own interests? </P>
<P>The article is disappointing because it fudges on the hard issues that the situation raises.</P>
<B><P>Peter Cogan, </B>Seattle</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Operation Homeland Resistance</b></font></P>

<B><P><font face="Times New Roman, Times, serif" size="3">Dear <I>Socialist Worker,</I> </P>
</B><P>On Tuesday, May 5, I took part in Operation Homeland Resistance, a three-day civil disobedience action at the Immigration and Naturalization Service building in New York City to protest both the war abroad and the war at home. This event was coordinated by a coalition of groups, mainly representing immigrants and people of color--the communities most seriously affected by the racist homeland security laws.</P>
<P>With a total of 83 arrests for the three days, the action was a big success. I was among the 26 arrested on Tuesday and was one of only five held overnight at central booking. Throughout the three days of action, those chosen by police to be held overnight seemed largely to fall along the lines of people of color, visibly gender-variant people and notable activists.</P>
<P>My experience in prison overnight gave me a personal insight into the corruption of our criminal justice system. It further reinforced the need to connect the oppression of the people of Iraq by an unjust foreign government and the oppression of people in this country who are suffering because of budget cuts, racial profiling and politically repressive laws.</P>
<P>This action is one step forward by the antiwar and social justice movements to continue the fight and step up the tactics of resistance in accordance with the increased level of repression by our government.</P>
<B><P>Rose Mishan, </B>New York City</P>



