<P><p><font face="Times New Roman, Times, serif" size="4"><b>Protest police killing</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul Dean</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">PORTLAND, Ore.--A multiracial throng of 1,500 marched through northeast Portland to protest the police murder of Kendra James. Last Monday, a grand jury cleared police officer Scott McCollister of any wrongdoing.</P>
<P>Kendra--a 21-year-old African American mother of two--was shot on May 5 while police tried to drag her out of a car stopped for a minor traffic violation. The cop said that he feared for his life, though Kendra was unarmed. After she was shot, the cops handcuffed her and left her lying in the street without medical attention because they said she was faking it.</P>
<P>"The cop says he feared for his life," Pastor Roy Tate told the crowd. "Well, we fear for ours!" The verdict left minority communities with the feeling that there will never be justice. The crowd swelled to such a large size because of grassroots organizing in the community--and they were joined by many people politicized by the antiwar movement.</P>
