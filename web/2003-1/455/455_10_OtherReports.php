<p><a name="Liberties"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend civil liberties</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Katie Ray</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">MADISON, Wis.--University of Wisconsin (UW) Madison students saw a recent victory against discrimination after hundreds of students mobilized against a $125 fee to be added to the tuition of international students.</P>
<P>This policy, which was to be implemented in the 2003-2004 school year, was supposed to cover the costs of Homeland Security's Student and Exchange Visitor Information System (SEVIS). In other words, international students were to pay for their own surveillance! UW students and supporters showed administrators that we would not stand for their unfair fee, forcing them to concede to "seek other funding sources" for the coming year.</P>
<P>International and domestic students alike quickly spread the word on campus and in the community, and the response was overwhelming. "Most people didn't really know anything about the USA PATRIOT Act and the implications of Homeland Security-type legislation, and this campaign was their first glimpse at these policies at work in their community," said Kate McCoy, a leading activist in the anti-SEVIS campaign. "Most people were shocked and appalled."</P>
<P>Sympathetic editorials appeared in local papers, 30 UW departments came out against the fee implementation, and the student government and the Madison City Council passed resolutions against it. Most important was the work of these students themselves, who got almost 1,500 people to sign petitions, held a well-attended rally and engaged in debates with administrators--all of which ultimately forced them to shelve their plans.</P>
<P>Everyone has a stake in fighting against these witch-hunts that undermine diversity and democracy on campuses and in communities. </P>
<P>While International UW students will no longer be required to pay for their surveillance under SEVIS, the system remains in place. We need to spread this fight to other campuses and communities that will face similar battles in the year to come and show those in charge that we won't stand for their divisive tactics.</P>
