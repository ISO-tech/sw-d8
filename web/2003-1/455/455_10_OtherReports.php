<p><a name="NYC"></a><font face="Times New Roman, Times, serif" size="4"><b>New York City antiwar protest</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Wrigley-Field</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--"Stop the U.S. war machine, from Iraq to Flushing, Queens," chanted 150 demonstrators at a May 19 rally against the U.S. occupation of Iraq and the attacks on communities of color at home. Flushing is a predominantly Asian neighborhood that, like other areas with many immigrants, has faced INS special registration and racial profiling since the September 11 attacks.</P>
<P>Standing outside the Times Square military recruitment station, speakers criticized military recruitment in high schools. "If you are poor, of color, and in the city, you are 10 times more likely to get a recruitment call from the military than from colleges," said Sabrine, a representative from the antiwar, antiracist group Third World Within. "Today we want to draft people into a new army of peace, justice and resistance," said Sabrine, speaking alternately in English and Spanish to the multiracial and multilingual crowd. </P>
<P>The rally was one of several events on May 19 in cities around the country to oppose the occupation of Iraq and the repression at home. The day was called by the group Racial Justice 911 to coincide with the birthday of Malcolm X, and many speakers drew parallels between past liberation movements and those today.</P>
