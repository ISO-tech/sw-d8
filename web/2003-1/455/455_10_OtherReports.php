<p><a name="Beats"></a><font face="Times New Roman, Times, serif" size="4"><b>Beats for Peace</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jesse Hagopian</b></font></P>
<P><font face="Times New Roman, Times, serif" size="3">WASHINGTON--The Beats for Peace tour came to a close this week after a week-long East Coast tour. Sponsored by the American Friends Service Committee, the tour featured some of the biggest names in hip-hop sounding off against war. </P>
<P>On May 11, Medusa, Pharoahe Monch, Cee-Lo, Schoolz of Thought, Maceo of De la Soul and Supernatural all rocked the stage with the message of stop the war--the war on our schools, the war on democracy and the war on the world. The event provided a forum for many different activists and organizations working against the war at home and abroad, locally and nationally.</P>
