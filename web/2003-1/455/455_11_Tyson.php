<P><font face="Times New Roman, Times, serif" size="5"><b>"Corporate greed has got to stop at Tyson"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Peter Couvares</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 30, 2003 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">JEFFERSON, Wis.--After 14 weeks into a strike, workers at the Tyson plant here are learning first-hand about the war at home. Meat-processing giant Tyson Foods made over $1.8 billion in gross profit last year and CEO John Tyson personally took home over $7 million in salary, bonuses, and stock awards. But in February, management proposed ten major concessions to workers here, cutting wages, health care, pension benefits, sick days, and more.</P>
<P>In response, defiant workers at the plant voted 400 to 9 walk out. Since then, not a single worker has crossed the picket line, even though Tyson has still refused to bargain with the union, United Food and Commercial Workers Local 538.</P>
<P>"For a lot of people, this is their livelihood," explained Tom Schicker, a 38-year veteran of the plant who was one of over 30 workers forced to retire early days before the strike to avoid losing their benefits. "If we don't stop them here, it's just the starting point for what they'll do."</P>
<P>Decades ago, meatpacking was a sought-after job in Jefferson.  The plant had to pay high wages due to the difficult work. "It was a hard job to get into &#151; you had to know someone," explained Ed Meyer, a 37-year veteran of the Jefferson plant. "Everyone wanted to work here."</P>
<P>Over the last few decades, however, fierce competition has led to consolidation in the meat processing industry and the  growing attacks on workers that came with it. Companies like IBP and Tyson opened new plants with non-union labor, producing product more cheaply than their smaller, less-efficient competitors.</P>
<P>Year after year, the owners kept profits high in part by driving smaller companies out of business or buying them up. This is what happened to the Tyson plant in Jefferson, which started as a small independent company.</P>
<P>It was sold four times in two decades in a series of buyouts by larger and larger corporations like Wilson and IBP until finally being sold in 2001 to Tyson, now the largest meat producer in the country. These acquisitions allowed Tyson to grow fast and record enormous profits even as its workforce was being paid less and less.</P>
<P>In the early 1980s, almost half of all meatpacking workers were unionized, starting at an average of $10.69 an hour. But non-union workers were starting at $8.25 an hour, and by 1987, unionized workers made up only 21 percent of the industry.</P>
<P>Now there are only a few giant meatpacking companies left, and they can't easily increase profits just by growing larger--so they're asking workers to sacrifice.</P>
<P>Never mind that John Tyson's 2002 bonus alone could pay for health care for everyone in the Jefferson plant. Instead, Tyson wants people like Gordon Jacobs, who's been working at the plant for 28 years, to give up his benefits. "This is the kind of company we're dealing with," he said.  "Corporate greed has got to stop."</P>
<P>The drive for profit has meant not only lower wages, but a steady worsening of working conditions. According to the United States Department of Agriculture, meatpacking has the highest rate of occupational illnesses and injuries of all U.S. industries.</P>
<P>Tyson is one of the worst offenders. In 1999 alone, seven Tyson workers were killed in workplace accidents. Two workers at a plant in Kentucky died in a pit of poultry entrails, and a worker at another plant was crushed under an elevator.</P>
<P>Tyson was even fined by the U.S. Department of Labor for child labor violations after the death of a 15-year-old at an Arkansas plant exposed the practice. Despite these preventable deaths, company executives saw an increase of 78 percent in their year-end bonuses.</P>
<P>But Tyson has always had help in high places. Hillary Clinton was a former lawyer for the company, and  former President Bill Clinton oversaw deep cuts in the numbers of government inspectors who monitor workplace safety.</P>
<P>And in 1998, a Tyson executive was convicted and sent to prison for bribing agriculture secretary Mike Espy, but was pardoned by Bill Clinton. Support like this has allowed Tyson bosses to think they can get away with anything.</P>
<P>But the workers in Jefferson are fed up. As striker Ed Meyer argued, "it's about time for all the unions to start walking." Added Rick Schlesner, "--like they do in Germany. One plant shuts down, they all shut down." This is the kind of solidarity that can win in Jefferson--and around the country.</P>

