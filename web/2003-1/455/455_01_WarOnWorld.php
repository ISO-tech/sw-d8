<P><font face="Times New Roman, Times, serif" size="4">Bush the conquerer</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Stop his war on the world</b></font></p>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 30, 2003 | Page 1</font></p></P>

<P><font face="Times New Roman, Times, serif" size="3">BUSH THE conqueror is getting ready to lord it over his new colonial subjects. His world tour that begins this week is expected to end with a flag-waving photo op in front of U.S. troops in Kuwait or Qatar--to celebrate victory in the war on nearby Iraq.</P>
<P>Of course, Bush doesn't plan on setting foot in the vanquished oil colony itself. That just wouldn't make for good pictures. Close to two months after the end of the war, electricity and drinking water still haven't been fully restored in Iraqi cities, the country's hospitals remain in a shambles, and new protests against the U.S. military occupation erupt with each passing week.</P>
<P>But that hasn't stopped the administration from planning the next stop in the "war on terror." Bush's appearance will also send a message across the Persian Gulf to the Iranian government--which, according to press reports, has been targeted by the White House for "destabilization" for supposedly harboring al-Qaeda fugitives.</P>
<P>Before his trip to the Middle East, Bush will attend a summit of world leaders in the luxurious resort town of Evian, France. There, he'll send a message to the other governments in the Group of Eight club of rich countries--that there's only one superpower, and no one had better get in the way.</P>
<P>The Bush administration was already doing its best to snub the French and German governments in advance of the summit, in retaliation for their opposition to the U.S. and British invasion of Iraq without United Nations approval. After announcing that the French army would be banned from upcoming international military exercises, Secretary of State Colin Powell told reporters that the White House is still considering whether France will face more "consequences" for crossing Washington. And National Security Adviser Condoleezza Rice told a German magazine last week that the Bush administration is "going to work around the chancellor [Gerhard Schroeder]. It's better to leave him out."</P>
<P>The Bush gang is oozing overconfidence in its drive to expand U.S. power--against enemy and ally alike.</P>
<P>Meanwhile, at home, they're carrying out new attacks on workers and the poor--from tax cuts that will mostly benefit the super-rich, to new plans to slash government health care, welfare and education programs. The sheer arrogance of the Bush administration's victory tour for U.S. imperialism is infuriating. It's time to stand up to this drive for plunder and conquest--and say no to George Bush's war on the world.</P>


