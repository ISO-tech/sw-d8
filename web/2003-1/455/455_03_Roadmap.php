<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A "peace" that's designed to fail</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 30, 2003 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">AROUND THE world, front-page headlines blared the news last weekend of a historic breakthrough for Middle East peace. Don't believe it.</P>
<P>The Israeli government voted to endorse the U.S.-backed "road map" to Middle East peace--and for the first time officially recognized the claim of Palestinians for their own state. "The moment has arrived to divide this tract of land between us and the Palestinians," announced Prime Minister Ariel Sharon.</P>
<P>But after the seizure of Palestine from the Palestinians in 1948, the occupation of Gaza and the West Bank starting in 1967, and a war to crush two Palestinian uprisings in the last 15 years, why would one of the most right-wing governments in Israeli history suddenly decide to make such a sweeping "concession"? The answer is that this is no concession.</P>
<P>Sharon and his hard-line cabinet understand their vote as a diplomatic maneuver, not a plan to actually implement a Palestinian state. "Israeli policy today is focused more on two main aims," writes mainstream military analyst Ze'ev Schiff in Israel's <I>Ha'aretz </I>newspaper. "One of them is to make a good impression in Washington, but the major objective is to do everything possible so that Israel will not be blamed for the failure of the current move, at the center of which is the road map. That is, so that the failure will be perceived as the Palestinians' handiwork...If this is indeed a historical opportunity, what is Israel's contribution, apart from sitting in the bleachers?"</P>
<P>In truth, Israel isn't just sitting on the sidelines. It is doing its best to crush the Palestinian resistance now. Shortly before the cabinet vote, Israel Defense Force (IDF) soldiers stormed the West Bank city of Tulkarem with dozens of tanks and armored personnel carriers, carrying out house-to-house searches and terrorizing the local population in a pre-dawn raid. A few days earlier, two Palestinian journalists--in a clearly marked media vehicle--were badly beaten by soldiers.</P>
<P>These are only the latest atrocities in Israel's iron-fisted crackdown on the Palestinian Intifada, or uprising, that began in September 2000. Still, half a century of Israeli military operations haven't wiped out the Palestinian resistance.</P>
<P>But Israeli officials figure that if the military can't crush the opposition, another favorable outcome is also possible--a fratricidal war between rival Palestinian factions. And the road map is the perfect tool to help bring this about.</P>
<P>"[B]efore Israel is required to do anything, the Palestinian security services, eviscerated by two years of pitiless Israeli attacks...must wage a relentless war against the Palestinian factions that attack Israeli occupation forces and settlers in the occupied territories as well as civilians inside Israel," writes Palestinian author Rashid Khalidi. "Palestinians complain that this means starting a Palestinian civil war before there is any indication that the Sharon government, dominated by hard-line supporters of the extension of settlements and the continued repression of the Palestinians, will do any of the things that are required of it."</P>
<P>Don't buy the claim that Middle East "peace" is around the corner. There can never be a genuine peace without justice--and that requires a secular, democratic state in all of Palestine where Jews, Arabs, Muslims and Christians all have equal rights. The U.S.-backed "road map" doesn't go there.</P>


