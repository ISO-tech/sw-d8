<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 30, 2003 | Page 11</font></p>

<P><font face="Times New Roman, Times, serif" size="4"><b>Oakland teachers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jessie Muldoon, Oakland Education Association</b></font></P>

<P><font face="Times New Roman, Times, serif" size="3">OAKLAND, Calif.--Without a contract, and facing a state takeover, about 70 teachers rallied May 21. After protesting in front of an Oakland public high school, the teachers marched to a busy intersection to leaflet cars about the crisis in the school district. The teachers carried signs calling for no layoffs or cuts to educational programs--and received overwhelming support from passers-by.</P>
<P>The leafleting was organized by the Oakland Education Association (OEA) to build community support, and to offset media attacks on the union. The <I>Oakland Tribune </I>has blamed the teachers for the current budget deficit and the state takeover of the district. But the union has stood firm in its argument that teachers are in no way responsible for the budget crisis.</P>
<P>At the rally after the leafleting, OEA President Sheila Quintana addressed the crowd. Responding to the district's claims that everyone has to share in the cutbacks, she told the crowd, "We didn't ask the district to mismanage $100 million."</P>
<P>She also decried the district's plans to lay off 250 support staff. "We have to fight," she said. "We need everybody! We need custodians, we need food service workers, we need IAs [instructional assistants], we need clerical workers, we need secretaries, we need everybody!"</P>
<P>Teachers plan to keep the pressure on the district for the remainder of the school year. Every school in Oakland needs to organize on site union meetings this week in order to organize maximum turn out for the OEA membership meeting on Wednesday, June 4. </P>

