<P><font face="Times New Roman, Times, serif" size="4">Bayer offloaded deadly drug to poor world</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The price of their profit</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 30, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">REVELATIONS THAT a division of the drug giant Bayer knowingly sold dangerous, AIDS-causing medicine to poor countries demonstrate the deadly depths pharmaceutical companies will go to make a profit.</P>
<P>According to the <I>New York Times, </I>Cutter Biological, a division of Bayer, offloaded millions of dollars of blood-clotting medication to Latin America and Asia in the mid-1980s, even though it carried a high risk of transmitting AIDS to hemophiliacs. The medicine, called Factor VIII concentrate, was made from the plasma of donors when there wasn't yet a screening test for the AIDS virus.</P>
<P>When it became clear in 1984 that the drug was infecting hemophiliacs with HIV, Cutter took the medicine off the market in the West--but continued to sell it overseas so as not to get stuck with stockpiles of old medicine. Documents also show that the company continued to produce the dangerous version of the drug for several months to fulfill existing fixed-price contracts for the drug--because it was cheaper to produce.</P>
<P>And the company got help from federal regulators. In May 1985, Food and Drug Administration blood-products regulator Dr. Harry Meyer Jr. asked Cutter to take the medicine off the market--but also requested that the issue be "quietly solved without alerting the Congress, the medical community and the public," according to Cutter's records of the meeting.</P>
<P>While it's unknown how many died as a result of Cutter's deadly cost-cutting, in Hong Kong and Taiwan alone, more than 100 hemophiliacs contracted HIV after using their medicine. "They did not care about the lives in Asia," said Li Wei-chun, whose son died in 1996 of AIDS at the age of 23 in Hong Kong. "It was racial discrimination." </P>



