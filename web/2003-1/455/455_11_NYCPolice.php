<P><font face="Times New Roman, Times, serif" size="5"><b>Stop New York City's killer cops</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Peter Lamphere</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 30, 2003 | Page 11</font></P>

<P><font face="Times New Roman, Times, serif" size="3">NEW YORK--Twice in the past week, the racist NYPD killed two innocent, unarmed civilians. On May 16, police--acting on a false tip about drug dealing--broke down the door to 57-year-old Alberta Spruill's apartment and ignited a concussion grenade. </P>
<P>Despite Spruill's protest that she had a heart condition and was the wrong person, they handcuffed her. Literally frightened to death, she died of heart failure less than an hour and a half later. </P>
<P>New York politicians tried to appear apologetic. Mayor Michael Bloomberg spoke at Spruill's funeral, taking responsibility for the cops' mistake. But the day before the funeral, the city re-authorized the use of grenades in carrying out search warrants. </P>
<P>And like in previous police brutality cases, the city investigated Spruill's background to see if it could dig up any dirt on her. There was no dirt to dig up. Spruill was an active member of her tenants' association and a regular churchgoer. She was also a 29-year veteran of the city workforce, and a member of both AFSMCE District Council 37 and the Coalition of Black Trade Unionists.</P>
<P>A week later, the cops struck again, this time shooting an unarmed African immigrant four times--once in the back.  Once again, the victim, Ousmane Zongo, had nothing to do with the crime that police were investigating--a CD-counterfeiting operation based in the same warehouse where Zongo worked as a repairer of African sculptures. Just like Spruill, Zongo was a well-known community member. He worked long hours to send money back to his family in Burkina Faso in West Africa. </P>
<P>Small protests against these outrages have already erupted. In Spriull's neighborhood in Harlem, thirty people organized by members of her church marched on the 116th precinct on May 20. Another protest organized last weekend drew 200 people for a march from Spruill's apartment down Harlem's Malcolm X boulevard, chanting "No justice, no peace!"</P>
<P>Juanita Young, the mother of Malcolm Ferguson, who was shot by the police three years ago, expressed the outrage of the marchers. "They took my son away from me," Young said. "Now, they're taking mothers away from their sons." </P>
<P>The protests must grow if we are going to have an impact on the racist policies of the NYPD. Another protest is planned for May 27, starting again from Spruill's apartment. As Sarah Bailey, vice president of the tenants' association that Spruill belonged to, told protesters: "As long as we stick together as a community--Black, Hispanic, white--we can get something accomplished and strike a mighty blow."</P>

