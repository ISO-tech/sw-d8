<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><br>
<font face="Times New Roman, Times, serif" size="4">United Nations gives its blessing to occupation of Iraq</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>A colony by any other name</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 30, 2003 | Page 3</font></p>

<P><font face="Times New Roman, Times, serif" size="3">IF IT wasn't obvious already, the U.S. is now the official occupier of Iraq. On May 22, the United Nations (UN) Security Council approved--by a 14-0 vote--a resolution giving the U.S. and its sidekick Britain the authority to occupy and "rebuild" Iraq.</P>
<P>So much for Germany, France and Russia's opposition to the U.S.-led invasion a few months ago. "The war has taken place," French Foreign Minister Dominique de Villepin told France Inter radio. "Now it's time to restore the unity of the international community." In reality, European leaders are trying to "restore" their hands on some of the loot from postwar Iraq--and the price for that was throwing their support behind a resolution that gives UN blessing to the new U.S. oil colony.</P>
<P>A major point of the resolution was the lifting of economic sanctions, which were imposed on Iraq 13 years ago after Saddam Hussein's regime invaded Kuwait. Over the last decade, the embargo destroyed what was left of Iraq's infrastructure after the 1991 Gulf War's punishing air campaign. Activists around the world fought long and hard for ending the deadly sanctions.</P>
<P>But it was pure hypocrisy for some Security Council members to spout their "concern" for the Iraqi people as a justification for backing the resolution. "[W]e cannot undo history," said German ambassador Gunter Pleuger. "We are now in the situation where we have to take action for the sake of the Iraqi people."</P>
<P>What a fraud! The suffering of the Iraqi people didn't matter to the war makers in Washington until they wanted to exploit Iraq's oil wealth, and it didn't matter to other world leaders either--until it seemed that they might be cut out after the U.S. takeover. When news of the end of sanctions reached Basra on May 20, residents celebrated, according to Alex Renton, of the aid group Oxfam.</P>
<P>But there's more to the story. That same day, "doctors and nurses demonstrated at the United Nations and at the British military base," Renton wrote in Scotland's <I>Sunday Herald. </I>"Their banner read: 'Restore security, water and electricity, or all the doctors and nurses will go on strike.'"</P>
<P>Judging from the occupation so far, the last thing that the U.S. government should be in charge of is the future of the Iraqi people. Hospitals are still in chaos, and food and clean water are scarce. Yet the U.S. government, with all its vast resources, hasn't lifted a finger.</P>
<P>With the country plagued by poverty and hunger, conditions are ripe for looting and violence. And the worst gang by far is the U.S. military.</P>
<P>In April, in the city of Falluja, west of Baghdad, U.S. troops fired on anti-occupation demonstrators twice in one week, killing at least 15 people. Last week, U.S. troops were at it again in Falluja, firing tank shells indiscriminately around the city and forcing their way into people's homes after U.S. soldiers were fired on. "They went crazy, they fired everywhere," Safi Jaber told Reuters.</P>
<P>In the oil-rich city of Kirkuk in northern Iraq, residents got an idea last week of what Washington means by "democracy" during local elections. "In preparation for the vote, U.S. forces surrounded the city hall with tanks and spools of barbed wire," the <I>Washington Post </I>reported. "Iraqis whitewashed the building, hung up red, white and blue bunting--and then took it down, because those are not the colors of the Iraqi flag. "And when Major Gen. Ray Odierno entered the torpid auditorium, the delegates were instructed to stand."</P>
<P>From the delegates who voted to the candidates they voted for, the U.S. called the shots "We know who is running the show," one attendee told the <I>Post</I>.<I> </I>Just to underline the point, U.S. officials announced that they were pushing back the timetable for Iraqi self-rule. "We are talking about sometime in July," said Washington's top dog, chief administrator Paul Bremer, who chose to make this his first order after the UN resolution sanctioning the occupation was passed.</P>
<P>This is what the United Nations is fronting for--a U.S. occupation in which all opposition is met with an iron fist. Washington won't leave Iraq until it has imposed its puppets and exploited the country's oil resources--unless it's forced out. That's why we oppose the U.S. occupation of Iraq--with or without UN permission.</P>



