<P><font face="Times New Roman, Times, serif" size="5"><b>Racist cop charged with lying in Tulia</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mike Corwin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 9, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE NARCOTICS cop responsible for the arrest of 46 people in Tulia, Texas, is a liar. That's the argument of prosecutors filing felony perjury charges against Tom Coleman, the rogue cop who spearheaded a 1999 drug sting in which 10 percent of Tulia's African American population was rounded up.</P>
<P>No evidence supported Coleman's accusations against those arrested. Nevertheless, 38 men and women were convicted on drug trafficking charges on the basis of his testimony.</P>
<P>The perjury charges against Coleman stem from contradictory statements he gave at a special hearing in Tulia last month. The hearing was commissioned by the Texas Court of Criminal Appeals, Texas' highest appeals court and well-known as a rubber stamp for some of the state's worst miscarriages of justice. At the hearing, the presiding judge recommended that the state high court reverse the sentences of all those convicted.</P>
<P>Thirteen victims of the Tulia drug sting still languish in jail--including Joseph Moore, a 61-year-old hog farmer afflicted with severe diabetes, and Kizzie White, a mother of two. The court isn't expected to rule on these cases until the fall. Prosecutors and defense attorneys say they are working on a deal for the 13 still behind bars to be released on bond.</P>
<P>While the exposure of Coleman's lies is a welcome development, he is only the tip of the iceberg in the racist "war on drugs" that allowed this travesty to happen. As <I>New York Times</I> columnist Bob Herbert wrote, the drug task force that Coleman worked for "squander[s] millions of dollars on amateurish investigations that snare mostly low-level offenders, and they tend to focus like lasers on people who are black or Hispanic."</P>
<P>Justice can't wait for these victims of the racist "war on drugs."</P>

