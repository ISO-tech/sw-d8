<P><font face="Times New Roman, Times, serif" size="4">Iraqi protesters shot down by U.S. ''liberators''</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The new dictator of Iraq</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 9, 2003 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>IRAQIS GATHERED in the town of Falluja last week to exercise their right to free speech. And they were gunned down by their U.S. "liberators"--not once, but twice.</P>
<P>The massacres began April 28, when protesters gathered in front of a school taken over by U.S. troops. Soldiers opened fire, killing 15 people in the crowd--three of them boys under age 11--and wounding dozens more.</P>
<P>Two days later, protesters again confronted U.S. troops--and soldiers again opened fire. "We could hear the bullets screaming over our heads," reported London's <I>Daily Mirror</I> reporter Chris Hughes. "Explosions of sand erupted from the ground--if the rounds failed to hit a demonstrator first. Seconds later, the shooting stopped, and the screaming and wailing began. One of the dead, a young man, lay face up, half his head missing, first black blood, then red spilling into the dirt. His friends screamed at us in anger, then looked at the grim sight in disbelief."</P>
<P>As Khazal Abdel Hadi told the <I>Los Angeles Times</I> from his hospital bed: "They just started shooting people. We had an agreement that we could hold a peaceful protest--we just wanted to tell them to get out of the center of the city." So this is the U.S. government's idea of "democracy" in Iraq--protest, and we'll shoot you down in the street.</P>
<P>Afterward, the Pentagon's man in charge, retired Gen. Jay Garner, had a message for reporters who questioned him about the anti-occupation demonstrations. "We ought to be beating our chests every day," Garner declared. "We ought to look in a mirror and get proud and stick out our chests and suck in our bellies and say: 'Damn, we're Americans!'" </P>
<P>For anyone who believes that U.S. troops have to stay in Iraq or "the country will collapse into chaos," the truth is plain. The U.S. Army brought the chaos with it. Washington's war makers didn't invade Iraq to bring democracy, and they aren't occupying the country to stop chaos. They are there for oil and empire--no matter what the human toll.</P>
<P>The people of Iraq have the right to decide for themselves how their country is run--and U.S. troops make it impossible for them to do so. That's why we say no to war and occupation. U.S. troops out of Iraq now!</P>


