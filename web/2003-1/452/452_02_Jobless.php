<P><font face="Times New Roman, Times, serif" size="5"><b>Bush offers no help for the jobless</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 9, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. unemployment rate jumped to 6 percent in April with the loss of another 48,000 jobs nationwide, the 33rd consecutive monthly decline. Officially, joblessness has matched an eight-year high. But the job loss statistics don't tell the whole story.</P>
<P>Labor Department figures also show that in April, the average work week fell to 34.0 hours in March, manufacturing hours declined to 40.5 and overtime hours dipped as well. Companies "have cut so many jobs at this point that some of them can't cut any more, and then they cut hours," Sharon Stark, a Wall Street analyst, told the <I>New York Times</I>. "It's not an encouraging sign."</P>
<P>Tim O'Neill, chief economist for the Bank of Montreal/Harris Bank, told Reuters: "[I]f you look at the overall report, while the headline employment decline was actually smaller than we expected it to be, the rest of the report was certainly more negative. No matter how you cut this report, there's nothing in it that would suggest that the labor market is on the verge of picking up."</P>
<P>The government's statistics don't even count the estimated 2.8 million workers who have run out of unemployment benefits or who stopped looking for work in the bleak job market. And more than 4.7 million people wanted full-time work last month but could find only part-time work--up 50 percent since 2000. If these people were included in the unemployment rate, the number would double.</P>
<P>Among people still looking for work, the average length of unemployment increased to 19.6 weeks in April, the longest since 1984. Even temp work is becoming harder to find. The number of temporary employees nationwide dropped 14,000 to 2.8 million--part of a 20 percent decrease over the last three years.</P>
<P>Bush seized on the new job losses as an excuse to push his tax cut proposal. "That 6 percent should say loud and clear to members of both political parties in the United States Congress--we need robust tax relief so our fellow citizens can find a job," he told one audience at military contractor in Santa Clara, Calif.</P>
<P>Who is he kidding? The Bush tax plan won't give "relief" to anybody except the already superrich. The Bush administration isn't planning to lift a finger to help the 1.9 million jobless who are about to lose their benefits completely on May 31.</P>
<P>"People want jobs; they don't want unemployment checks," said White House spokesperson Claire Buchan. In Bush's America, they won't get either.</P>


