<P><font face="Times New Roman, Times, serif" size="4">Sick consequences of the budget crisis in Oregon</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Giving blood to pay for schools</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul Dean</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 9, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>PARENTS AND teachers at an alternative school in Eugene, Ore., are planning to sell their blood plasma to raise money. That's how desperate the schools crisis has become in parts of the U.S.--even as George W. Bush pushes his plans to give new tax breaks to the already superrich.</P>
<P>Oregon, which has the highest unemployment rate in the entire country, is especially hard hit. In Portland, the state's largest city, a near-strike ended with a contract where teachers work 10 days for free each school year.</P>
<P>Schools in the nearby Hillsboro district will have 17 days lopped off their school year. And at a small alternative school in Eugene, the Family School, parents came up with the idea of selling blood plasma to raise $73,000 to pay for a teacher position lost through cuts. "They give so much as it is," said parent Catherine Flynn-Purvis. "The sweat and tears are not enough--now it's got to be blood, too."</P>
<P>A spokesperson for Aventis Bio-services, which will collect the plasma, called the idea a "win-win situation." But this certainly isn't a way to win stable funding for Oregon schools or any other service.</P>
<P>The pain isn't evenly distributed. At one Portland school with well-off parents, a recent auction raised more than $50,000 in a single evening. And while voters in Multnomah County, which covers the Portland area, will soon be asked to pass a temporary tax levy to help fund schools or face more cuts, the state legislature is considering a proposal to eliminate state income tax for venture capitalists for up to seven years. </P>
<P>Ex-Enron subsidiary Portland General Electric posted after-tax profits of $66 million last year--but paid just $10 in state corporate income tax for 2002, the minimum that a corporation must pay.<I> </I>In the year 2000, 80 percent of Oregon corporations qualified for the minimum. So the poor are paying far more in taxes than the state's biggest corporations!</P>
<P>"It's a bizarre and poignant place we've come to, when we're reduced to donating our bodily fluids to support our schools," said another parent at the Family School. "It's definitely our last stand."</P>
<P>Oregon's working people shouldn't be forced to pay for this crisis. The rich have the money; it's time for them to pay up.</P>

