<P><font face="Times New Roman, Times, serif" size="4">San Francisco officials won't punish brutality</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Covering up for cops</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 9, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>One has to wonder what kind of world we're living in when a man has to call 911 to save himself from the police. That's what happened last November, when Adam Snyder and Jade Santoro were viciously beaten by off-duty officers Alex Fagan Jr., Matthew Tonsing and David Lee. </P>
<P>Snyder, a bartender, and his friend, Santoro, were standing outside a bar just after closing when the three rookie cops walked by and demanded the bag of steak fajitas that Snyder was holding. A fight ensued, and while watching helplessly as Santoro was being pummeled on the ground, Snyder dialed 911.</P>
<P>San Francisco Police Department (SFPD) Chief Earl Sanders, his number two man, Alex Fagan Sr. (Alex Jr.'s father) and the rest of police top brass have since been indicted for covering up the incident. </P>
<P>But despite the grand jury's unanimous decision, Mayor Willie Brown and the Police Commission are fighting it, allowing Sanders to go on sick leave (and collect $17,000 a month!) and releasing seven "on their own recognizance." </P>
<P>Not only that, but District Attorney Terence Hallinan is now expected to drop all obstruction of justice charges against these men, proving that with enough money and power, one can get away with just about anything.</P>
<P>Fagan Jr. has a long history of violence and aggression. The <I>San Francisco Chronicle</I> reported that he used force at least 16 times in a 13-month period, sending six people to the hospital for critical injuries. One man alleges that Fagan kicked him in the head. Another suffered broken ribs and a punctured lung. Yet Fagan continued to get away with his brutality until last November.</P>
<P>The reason for the cover-up is simple: this is just the tip of the iceberg. Sanders and Co. know that police brutality and civil rights violations are an everyday practice in the department. For decades, the SFPD has treated serious officer misconduct leniently and promoted to top posts cops with troubling records. </P>
<P>Most SFPD victims are poor and working-class people of color. And the SFPD is but a microcosm of a system that is flawed at the national level, where the rich and powerful are rewarded for their tyranny and applauded for their crimes against humanity. </P>
<P>Fagan and George W. Bush are brothers in the same fraternity, and if the oppressive regimes of these privileged men are ever to end, the city, the nation and the world has to start standing up and taking notice.</P>
<B><P>Dinaz Kardooni, Poly Manoli and Eduardo Capulong, </B>San Francisco</P>


