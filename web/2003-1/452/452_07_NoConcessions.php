<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>No concession to the U.S. occupation</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Ganesh Lal</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 9, 2003 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. occupation of Iraq has thrown at least some antiwar activists and organizations into confusion. There are a series of questions about the nature of the occupation--and objections to calls for an end to it.</P>
<P><B>"The U.S. can't pull out. That would be irresponsible."</B></P>
<P>This assumes that the U.S. can be relied on as a "responsible" force in the Middle East.</P>
<P>In one sense, this is true. The U.S. has been responsible--for arming and funding the state of Israel, the only known nuclear power in the region. It has been responsible--for the systematic trampling of human rights in the region through its unstinting support for dictatorial regimes in Egypt, Saudi Arabia, Kuwait and Turkey. It has been responsible--for genocidal sanctions on Iraq over the past decade that, as is commonly acknowledged today, killed hundreds of thousands of innocent Iraqis. </P>
<P>The U.S. ought to be held responsible for these crimes--and not relied on to be a benevolent force in Iraq.</P>
<P><B>"If we pull out now, there would be chaos in Iraq."</B></P>
<P>But the U.S. caused the chaos with its war on Iraq. Entire cities like Baghdad and Basra were plunged into darkness, without electricity or water for weeks. Schools and neighborhoods were destroyed. Museums and libraries were looted while U.S. troops zealously guarded the Ministry of Oil.</P>
<P>Second, there is little that distinguishes this argument from the idea of the "white man's burden" that formed the ideological backbone of old-style colonialism. The corporate media have stoked these racist ideas, with images of Iraqis as "looters" or "fanatics." How can such people be relied on to rule themselves?</P>
<P>Antiwar activists who buy this argument would do well to read Thomas Friedman's April 30 <I>New York Times</I> column, where he argues that Iraqis cannot be governed by an "iron finger," only by an "iron fist." </P>
<P><B>"But people are starving--shouldn't the U.S. do something?"</B></P>
<P>Most aid organizations in Iraq have complained that U.S. troops are the main obstacle to their work. Meanwhile in Afghanistan, millions continue to live in squalid refugee camps and face starvation. The promised "rebuilding" of the country hasn't happened--though "combat operations" continue.</P>
<P><B>"A U.S. pullout would plunge the region into instability."</B></P>
<P>Again, the main cause of instability in the Middle East is the U.S. Rest assured that the U.S. government will try to achieve "stability"--with puppet regimes, military dictatorships and monarchies. This kind of "stability" will rule out self-determination for the people of Iraq. It will rule out popular control of the oil resources of that nation. It will rule out genuine democracy, for as George W. Bush has often reminded us, democracy is contagious.</P>
<P>A truly liberated Iraq poses a serious challenge to the "stability" imposed on the region by U.S. imperialism, as it would set an example to the peoples of Egypt, Syria, Saudi Arabia and, of course, Palestine.</P>
<P><B>"We have a duty to help the Iraqi people."</B></P>
<P>Iraqis themselves are saying "no thanks." Increasingly, U.S. soldiers have fired on anti-occupation protesters, killing and injuring dozens in Baghdad, Falluja and Mosul. Accepting that the U.S. occupation must continue in some form means supporting these crackdowns on Iraqi protesters, as the occupation can only succeed by crushing dissent. This is because Iraqis themselves are standing up against the occupation. So which side should we be on?</P>
<P>Opposition to the New American Empire is incompatible with support for the U.S. occupation of Iraq. Rather than hoping that Uncle Sam will make things better, we have to look to the power of the international antiwar movement. That solidarity was on display around the world on February 15. We shouldn't squander it by taking up the white man's burden all over again.</P>


