<p><a name="George"></a><font face="Times New Roman, Times, serif" size="4"><b>George Bush is the real brute and bully</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>I sent an article to your paper that was printed in January ("Held hostage by the racist injustice system," January 17). I'm really honored for your help.</P>
<P>Now, I really have to say that the war situation is out of hand. Ever since Bush got into office, I knew this would happen! All of this uncalled-for foolishness: the war, the starvation of children. I really think that Bush has a serious problem. </P>
<P>Communities are suffering and jobs are on the down-and-downer. Everyone knows that the U.S. has the upper hand in Iraq, but why bully on and push others around?</P>
<P>Bullies get roughed up in the end. Nine out of 10, you lose the fight if you started it. The "big people" should show some consideration and be human for a change. Personally, I think Bush is the real brute. No more blood for oil!</P>
<B><P>Cornell Milton, </B>Chicago</P>


