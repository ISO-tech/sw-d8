<p><a name="How"></a><font face="Times New Roman, Times, serif" size="4"><b>How will we stop wars?</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>Even before the war on Iraq began, San Francisco was host to antiwar marches bringing tens of thousands--and even hundreds of thousands--of people into the streets. This anger and radicalization spurred people to think about next steps and tactics in active dissent, with rallies slowly shifting from more passive and legal marches into forms of mass civil disobedience.</P>
<P>I say slowly shifting because there was a growing debate in the movement about the rationale for civil disobedience. For example, during one protest in San Francisco, we had enough people to take over the Bay Bridge and stop traffic. Activists hesitated about this idea for a number of reasons.</P>
<P>Some were afraid of being arrested or beaten by the cops. Others felt it would encourage the press to demonize the struggle. These are understandable concerns, but they need to be argued through, because the truth of the matter is that there comes a time where civil disobedience makes sense.</P>
<P>Many of us protesters are no longer new to the movement. Now is the time when we need to start trying out new strategies that actually put pressure on those in charge to listen to our demands.</P>
<P>For example, at San Francisco State University (SFSU), we staged a successful sit-in inside our administrative building. Three hundred students occupied the first floor of the building and demanded that the university president address our demands. The police were ready for us, but we stood our ground. </P>
<P>At the end of the day, however, we started to get smaller, so from there, our group openly agreed to call it a day. Even though we didn't get our demands met right away, I see this sit-in at SFSU as a success. Students Against War, the main organizers, were able to convince a portion of our campus of the need for civil disobedience. The fear of being arrested or harassed by the cops is legitimate, but there comes a point when we have to weigh what's more important--that fear or making change.</P>
<P>It's important to say that we are at a time now where the war in Iraq is over. </P>
<P>Many activists are in a state of demoralization, and the question of civil disobedience is not as important right now as boosting the politics and organization of the movement. But we haven't seen the last of Bush's war drive.</P>
<P>Ultimately, we are not going to end these wars just by dialogue or passive rallies. We are at a time where the stakes have been raised, and we as activists need to get more confrontational. </P>
<B><P>Sarah Levine, </B>San Francisco</P>

