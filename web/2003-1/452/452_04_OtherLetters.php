<P><font face="Times New Roman, Times, serif" size="4"><b>A hollow promise of U.S. "freedom"</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>From the window of my workplace on the Upper East Side of New York, I just saw a young Black man arrested while he was walking down Lexington Avenue. The reason? He was unable to produce a receipt from his GAP shopping bag to show police when they asked him for one (apparently, the cops think that Black people don't shop at the GAP--therefore, they can only shoplift). </P>
<P>In the course of the arrest, I overheard the cops admit that they had been tailing him for at least 10 blocks. This reminded me of something Rosa Luxemburg wrote: "Bourgeois class justice is the net through which rapacious sharks escaped with ease, while it caught in its pitiless meshes every small, helpless minnow that ventured beyond the pale of capitalist law."</P>
<P>Instead of arresting the sharks who this very minute are committing mass murder and grand theft oil in Iraq, the cops are worrying about a Black man walking down Lexington Avenue with the "wrong" brand-name on a shopping bag. </P>
<P>And when they aren't going after minorities, they are attacking the antiwar movement. At Day X protests, three students from Hunter College were arrested after the police took it upon themselves to try to fence and trap protesters.</P>
<P>When Black people can't even walk down the street in peace in this country, is it any wonder that Iraqis don't believe the claims that the U.S. military is an "army of liberation"? When antiwar protesters are beaten, is it any wonder that Iraqis are skeptical when the unelected Bush promises them democracy? </P>
<P>When he promises "massive aid within 36 hours" and two trucks (out of a total of seven!) show up to feed all of southern Iraq, is it any wonder that Iraqis are fighting? When the U.S. decides--after destroying their cities and killing their loved ones--to sell water to Iraqis, is it any wonder that Iraqis are throwing grenades and not flowers at U.S. soldiers?</P>
<I><P>Socialist Worker</I> had it right when it said, "It's right to resist!" We have to keep organizing against the war and win the battle for democracy and liberation at home.</P>
<B><P>Pham Binh,</B> New York City</P>

