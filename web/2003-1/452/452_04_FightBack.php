<P><font face="Times New Roman, Times, serif" size="5"><b>We'll fight back against their school cuts</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 9, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>I recently read Dana Blanchard's letter about the attacks on schools in Manhattan Beach, Calif. ("Our schools under fire," April 25). I am writing to share another story of "the seeds of discontent" here in San Francisco. </P>
<P>Teachers at my elementary school have had enough. After midyear budget cuts and layoff slips were delivered to one-third of our staff, we could hardly believe it when the 2003-2004 budget was released.</P>
<P>The funds provided for our school next year are significantly lower than this year and do not provide enough money to rehire all of our staff. To make matters worse, the School Site Council committee (composed of parents, teachers and paraprofessionals) is responsible for administering the budget. They want us to decide which positions get the ax and who has to look elsewhere for work next year!</P>
<P>We say, "Hell no!" Our School Site Council voted to submit the 2003-2004 budget in the red, with full funding for all current staff and services. We hope to inspire union members at other schools to make the same proposal to their School Site Councils. </P>
<P>We won't impose cuts on our brother and sister workers at our school. Let the district do its own dirty work!</P>
<B><P>Adrienne Johnstone,</B> United Educators of San Francisco, San Francisco</P>


