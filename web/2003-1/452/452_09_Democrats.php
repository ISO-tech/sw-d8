<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Are Democrats the answer to Bush?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 9, 2003 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LIBERAL AND progressive voices are sounding the alarm over what they see as impending disaster if George W. Bush is re-elected. "What is at stake, then, is nothing less than the attempted transformation of a tolerably free society into a variant of the extreme regimes of the past century. In that context, the national elections of 2004 represent a crisis in its original meaning, a turning point. The question for citizens is: Which way?" Sheldon Wolin wrote in the May 1 <I>Nation</I>.</P>
<P>Antiwar activists Carl Davidson and Marilyn Katz, in a discussion paper urging antiwar forces to turn to the 2004 elections, call for defeat of Bush's "War Party" or else "this party will move to control the world."</P>
<P>While anyone who opposed the war or who detests Bush's program would like to see him defeated, the problem comes with the options of what might replace him. For most who are looking to the 2004 election, this isn't a big problem. They say that "anybody but Bush" would be better.</P>
<P>But the people who say "anybody but Bush" don't mean it. Instead, they mean "anybody who seems to have a reasonable chance" of beating Bush. By this logic, even Sens. Bob Graham or Joe Lieberman--who criticized Bush for not pursuing war in the Middle East more aggressively--would be better!</P>
<P>At the same time, many liberals and almost all professional Democrats are now waging a pre-emptive war against the Greens, Ralph Nader or anyone else who might mount a credible electoral challenge from the left. Mimicking the right that attacks any criticism of Bush as treasonous, the "anybody but Bush" crowd is doing their best to demonize Nader and the Greens. "A third-party presidential challenge from the left would be reactionary and traitorous in the 2004 election," wrote Marty Jezer, on the CommonDreams.org Web site.</P>
<P>Yet, if they really want to assign blame for the advance of Bush's right-wing program, they should look no farther than the nearest mirror. Advocates of "anybody but Bush" usually cite the war in Iraq, the PATRIOT Act and the attacks on abortion rights as the three main reasons to get rid of Bush in 2004.</P>
<P>These are good reasons to oppose Bush. But, if you look closer, you find that each of these examples of Bush extremism wouldn't have succeeded without Democratic support.</P>
<P>Democratic leaders Gephardt and Daschle helped Bush pass the Iraq war resolution last October. Daschle engineered a last-minute deal with the White House that pushed through the PATRIOT Act--which only one senator voted against. And Democratic senators provided 16 votes and the margin of victory for one of Bush's central promises to the Christian right--the ban on late term abortions.</P>
<P>The conventional wisdom holds that any Democrat that aspires to the Oval Office can't get there on promises to reverse Bush's disastrous domestic policies alone. He or she will need a "credible national security" platform as well. That means a policy of maintaining and extending U.S. military supremacy.</P>
<P>That was the meaning of Sen. John Kerry's (D-Mass.) recent attack on former Vermont Gov. Howard Dean: "No serious candidate for the presidency has ever before suggested that he would compromise or tolerate an erosion of America's military supremacy." So don't expect the Pentagon budget to decline under a Kerry administration.</P>
<P>Dean has won the media mantel of "antiwar candidate" because he was the earliest and most vocal critic of Democratic reluctance to challenge Bush's tactics during his buildup to war.</P>
<P>Dean didn't really oppose the idea of war altogether. Last fall, he proposed that if Saddam Hussein didn't meet a 60-day deadline to comply with United Nations resolutions, "we will reserve our right as Americans to defend ourselves and we will go into Iraq."</P>
<P>As thousands in the antiwar movement debate whether they must work to force a "regime change" at the ballot box in 2004, they should keep these facts in mind.</P>

