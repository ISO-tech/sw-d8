<P><font face="Times New Roman, Times, serif" size="4">Latest victims of John Ashcroft's racist witch-hunt</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Disappeared in America</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 9, 2003 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>NICOLE COLSON reports on the latest attacks in John Ashcroft's war at home.</P>
</B><P>MAHER "MIKE" HAWASH disappeared on March 20, and only the federal government knows why. And they don't want to talk about it.</P>
<P>When the Feds came for Hawash, they wouldn't explain why they were arresting the 38-year-old Palestinian-born U.S. citizen in the parking lot of his office at Intel in Hillsboro, Ore. On the same day, FBI agents armed with assault rifles raided his family home, waking his wife Lisa and their three terrified children.</P>
<P>For more than five weeks, Hawash was held on a "material witness" warrant--with the government refusing to say why he had come under suspicion. During that time, he was held in solitary confinement at a federal prison and allowed out of his cell just one hour on weekdays. He could visit with his wife and lawyer just three times a week--and was strip-searched before and after each visit.</P>
<P>Then, on April 28, just one day before he was due to be released, Hawash was hit with charges of "conspiring to levy war on the United States" and providing "material support" to terrorists. The evidence? Hawash supposedly traveled to China at the same time as six other members of his mosque--with the aim, say the Feds, of then going to Afghanistan to fight against the U.S.</P>
<P>But Steve McGeady, Hawash's former manager at Intel and a coordinator of the campaign to win his freedom, says that the charges are nothing more than guilt by association. "I'm pretty surprised at how thin the evidence in the complaint was," McGeady told <I>Socialist Worker</I>. "It really amounts to a travel itinerary, some circumstantial evidence, and really not a great deal more."</P>
<P>Hawash reportedly came under suspicion because a neighbor called the FBI to report that he had been spending more time at home after September 11, "was not as friendly as usual" and "changed his attire from Western clothing to Eastern clothing, grew a beard and began attending a mosque on a regular basis."</P>
<P>As McGeady points out, "Mike would probably not be in the situation he's in had he not been an Arab American and had he not been Muslim." Fortunately, says McGeady, members of the community have rallied around Hawash--or he might still be languishing in prison.</P>
<P>Some 200 people turned out to an April 7 rally to demand his freedom, and as many as 175 protested on the day he was charged. "What's going on here amounts to some modern-day equivalent of a combination of Japanese internment and McCarthyism," said McGeady.</P>
<P>Hawash is just one of thousands of immigrants across the country caught up in the Justice Department's witch-hunt of Arabs and Muslims since September 11. There are plenty of new victims all the time.</P>
<P>In Indiana, a student at the University of Indiana recently reported that her father, a naturalized U.S. citizen originally from India, "disappeared" from a New York airport after returning from a business trip overseas. According to a letter in the <I>Indiana Herald Tribune</I> written by professors Audrey and John McCluskey, "Almost a week later, after much persistence by the family and their attorney, the FBI admitted that they 'have him,' but would provide no further information."</P>
<P>Even for those born in the U.S., the crackdown on civil liberties is hitting home. Jason Halperin, a member of the respected humanitarian group Doctors Without Borders, recently wrote about getting a "very small taste of what hundreds of South Asian immigrants and U.S. citizens of South Asian descent have gone through since 9/11."</P>
<P>At an Indian restaurant in New York City on March 20, Halperin and a friend were caught up in a raid by armed New York police officers. After making the customers move to the back of the restaurant, Halperin said that police made the Latino kitchen staff crawl on their hands and knees before patting everyone down.</P>
<P>When Halperin's friend insisted that the police had no right to hold them, he was told, "Yes, we have every right. You are being held under the PATRIOT Act following suspicion under an internal Homeland Security investigation." When Halperin requested a lawyer, a female officer put her finger in his face, and had the gall to say: "We are at war, and this is for your safety."</P>
<P>It turned out that--like so many others--the raid was a "mistake." But as Halperin concluded: "We are all vulnerable, so speak out and organize." Our rights depend on it.</P>
