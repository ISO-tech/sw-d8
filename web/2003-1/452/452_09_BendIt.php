<P><font face="Times New Roman, Times, serif" size="4">Bend It Like Beckham</I>'s antiracist message</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Not your typical feel-good movie</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Amy Muldoon</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 9, 2003 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>MOVIES: <I>Bend It Like Beckham</B>,</I> directed by Gurinder Chadha, starring Parminder Nagra, Keira Knightley, Anupam Kher and Jonathan Rhys Meyers.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>"ANYONE CAN cook aloo ghobi; who can bend a ball like Beckham?" Jess Bhamra, daughter of an Indian Sikh family in Britain moans about her fate in <I>Bend It Like Beckham</I>. Recently opened in the U.S., this movie became the largest-grossing British-made movie of all time, as well as the first film made by a non-white Briton to reach Number One at the box offices.</P>
<I><P>Bend It Like Beckham</I> is vibrant, funny and touching. It takes the genre of culture clash movies into more explicitly political territory than movies like <I>My Big Fat Greek Wedding</I>.</P>
<P>Jess Bhamra is a young woman with amazing talent on the soccer field, whose parents want a traditional path for their daughter: university and marriage. Her older sister, Pinky, is preparing for her wedding, and their mother embarks on a mission to teach her younger daughter how to be ready to marry--mainly by teaching her to cook traditional meals.</P>
<P>Jess' love for soccer is lived out through friendly games in the park with her buddy Tony and his mates and her obsession with David Beckham, the star for Manchester United. All this changes after she's approached by Jules, a member of the local women's team, the Hounslow Harriers.</P>
<P>What keeps the movie fresh is the great acting and writing, which combines lots of humor with more serious moments about the racism and sexism that Jess and her teammates face. Another aspect that sets <I>Bend It</I> apart is the role of Mr. Bhamra, played by Bollywood actor Anupam Kher, who has made more than 270 movies in India.</P>
<P>Movies about recent immigrant families often show parents pushing their children to follow in their footsteps, without any explanation--beyond the idea "that's just the culture." But the fights in <I>Bend It</I> over whether Jess can play soccer are not so much about wearing shorts as they are about how to respond to the racism Indians face in Britain.</P>
<P>Mr. Bhamra is driven by a desire to not let racists rob Jess of her passion for soccer as he was robbed of his for cricket. In the most moving scene in the film, Mr. Bhamra is forced to rethink what happened to him and the abandonment of his dreams.</P>
<I><P>Bend It Like Beckham</I> shines with real optimism about people in general--not just the very talented ones--because of our ability to stand up against all the oppression ordinary people face.</P>


