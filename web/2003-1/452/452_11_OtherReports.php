<P><font face="Times New Roman, Times, serif" size="4"><b>N.C. senate passes moratorium bill</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Brad Ward</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>GREENSBORO, N.C.--In a tremendous victory for anti-death penalty activists, the state senate passed a two-year moratorium on executions on April 30. The vote followed the mid-April release of a comprehensive report that exposed the racist injustice of North Carolina's death penalty.</P>
<P>Of those executed in North Carolina, 76 percent were Black. And an astonishing 98 percent of those in the sentencing phase of a capital trial can't afford a lawyer.</P>
<P>For years, the chief anti-death penalty activists here have been members of People of Faith Against the Death Penalty, a small organization that was essential to pressuring the state senate to finally act. This group, along with members of the Campaign to End the Death Penalty, have fought an uphill battle.</P>
<P>But the battle against capital punishment in North Carolina is far from over. The moratorium bill now moves to the state's House of Representatives, which has no required timetable for action.</P>
<P>Abolitionists need to use grassroots pressure to push the House to pass the bill. We need to send the message that we're not backing down.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>No cuts to veterans' benefits</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Leigh Taylor Johnson</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SALEM, Va.--Veterans from Tennessee, Kentucky, North Carolina and Virginia gathered outside of the Veterans Affairs Medical Center April 28 to protest the lack of funding for the VA hospital's post-traumatic stress disorder (PTSD) inpatient program.</P>
<P>The program, which helps veterans cope with war-related psychotrauma, has lost four staff members in the past year and is operating at 66 percent of capacity. Protester Rick Le Blanc, a three-time graduate of the program, said that without the PTSD treatment, he would "be six feet under." </P>
<P>But Bob Cox, the manager of the PTSD program, announced that veterans shouldn't expect a fully functioning program until at least July. In the meantime, new veterans of the Iraq war are returning home with serious psychological issues--memories of the slaughter they were directed to carry out on the Iraqi people.</P>
<P>The government that denies mental health care treatment to veterans is the same one that sloganeered to "support our troops." What hypocrisy! It's clear that the only policies that this administration really supports are the ones that line their own pockets!</P>

