<P><font face="Arial, Helvetica, sans-serif" size="2"><b>SAY NO TO BUSH'S WAR ON IRAQ</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Student activists form network in Chicago</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"Yes, we CAN"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 28, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SOME 300 students from 100 campuses gathered in Chicago last weekend to hold the first national conference of the Campus Antiwar Network (CAN). The meeting felt like history in the making, as students collaborated in forging a democratic, bottom-up, student-led antiwar network. </P>
<P>CAN decided on its points of unity, a structure and upcoming actions. While the bulk of the weekend was dedicated to hammering out these issues, the meeting also wedged in a great opening session, with Norman Solomon, author of the new book <I>Target Iraq: What the News Media Didn't Tell You</I>; Tomomi Nakamura, a student from Kokugakuin University in Tokyo and leader of the Japanese student antiwar movement; and Charlie Jenks and Sunny Miller of the Traprock Peace Center.</P>
<P>There were also 11 workshops, taking up everything from "The impact of Bush's war on the Middle East," with Middle East expert Rashid Khalidi, to "Lessons from past antiwar movements" and "Israel and Palestine."</P>
<P>"I think everyone was very encouraged by our accomplishments this weekend," said Kathleen Brown, a delegate from the University of Vermont, who was elected to CAN's national coordinating committee with 11 others. "Despite whatever frustrations people had with the process, which was new to many people, the overall attitude was great.</P>
<P>"People wanted to work through things constructively and get to the basics of organizing actions. Last year, in building the movement against the war in Afghanistan, the movement was much more embattled. But now, there's a great upswing coming out of the huge national demonstrations in San Francisco, New York City and around the world."</P>
<P>CAN adopted four points of unity--no war on Iraq, whether backed by the U.S. or the United Nations (UN); end the UN sanctions which have killed more than 1 million Iraqis; oppose the attacks on civil liberties and racist scapegoating at home; and money for jobs, education and health care, not for war. There were several debates about these points and others, but ultimately, these four points were supported by an overwhelming majority.</P>
<P>CAN is organizing for an April 5 national mobilization in a couple major cities that will follow the student-labor week of action already set for March 31 to April 4. Students hope this event will mark a contribution to the national antiwar movement, by forging collaboration between existing national formations such as United for Peace and Justice, International ANSWER, Not in Our Name, and U.S. Labor Against War.</P>
<P>CAN also decided to call for local actions, including student walkouts, when the U.S. bombing begins, and it endorsed the March 5 national day of action. Campus antiwar groups that want to get involved with CAN can go to antiwarnetwork.org on the Web.</P>
<P><STRONG>More organizing</STRONG></P>
<P>On February 21, 300 students, faculty and staff at the University of California at Santa Cruz, walked out of their offices and classrooms to protest the war on Iraq. The rally, which also commemorated the anniversary of the assassination of Malcolm X, focused not only on saying no to war but opposing racism as well. Activists are planning future actions against the war, including an emergency response when the bombs start falling.</P>
<I><P>Melanie Wilkinson contributed to this report.</P></I>

