<P><font face="Times New Roman, Times, serif" size="5"><b>NYC day care workers take lead in fighting concessions</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Michael Ware</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 28, 2003 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Day care workers are battling New York City Mayor Michael Bloomberg's attempts to make city workers pay for the city's budget crisis with givebacks and concessions.</P>
<P>Disgusted with the city's refusal to negotiate seriously, the union led a one-day walkout on February 12. The 6,000 workers--members of AFSCME District Council (DC) 1707--have been working without a contract since January 1, 2001. The union is asking for 4 percent annual raises, equal to those received by other municipal workers last year. </P>
<P>Yet, because those same municipal workers and city teachers have already begun negotiating for new contracts, Mayor Bloomberg wants to hammer DC 1707 and send the message that all unions must accept big concessions.</P>
<P>Bloomberg used the same logic during last year's private bus line strike in Queens. The city lost more money on missed fares than it would have paid to workers in wage increases in order to show the 34,000 workers at the Metropolitan Transit Authority that the city would play rough in their looming contract fight.</P>
<P>The day care workers' one-day walkout marks a break with the passive approach of DC 1707's prior administration. Raglan George Jr. defeated former Executive Director Josephine LeBeau in last year's elections. More progressive elements in 1707 are putting the membership at the core of the union's decision-making process. </P>
<P>Executive Board members in each of DC 1707's eight locals are made up of rank-and-file workers. "Any future strike activity will be decided by the members," DC 1707 Staff Organizer Ray LaForest told <I>Socialist Worker</I>. </P>
<P>Day care employees work at 378 non-profit centers, which receive funding from the city. Because they're not technically city employees, they're not prohibited from striking, as are all public sector unions under New York state's anti-union Taylor Laws.</P>
<P>"They are asking us to spend money we haven't got," whined New York City's Labor Relations Commissioner James Hanley to reporters. In reality, the city has received $136 million in federal funds for day care in the last two years while cutting spending on such programs by $92 million. The union has filed a federal lawsuit over the issue. </P>
<P>Bloomberg, who bought his way into office with a $4 billion personal fortune, countered by essentially offering 6 percent over five years, the loss of two weeks of vacation time and $15 co-pays on medical visits--in exchange for the union withdrawing the lawsuit! "[The offer] only incensed us," said George. "We're standing pat."</P>
<P>The largely African American and Latino workforce is one of the worst paid in the city, averaging $27,000 a year in one of the wealthiest and most expensive cities in the world. It's an outrage that city workers are expected to pay for the city's $4 billion budget deficit. New York Gov. George Pataki and former Mayor Rudy Giuliani cut taxes for corporations and the wealthy in the late 1990s, paving the way for the current crisis.</P>
<P>Now Bloomberg is refusing to tax corporations to make up the deficit, referring to them as "job-killing" taxes. And yet, he is threatening to layoff 12,000 municipal workers unless their union, AFSCME District Council 37, agrees to $600 million in concessions. </P>
<P>The one-day strike has already proven effective. The city had refused to bargain seriously until the union walked out. The strike illustrated who makes the city run--and the power that unions have to fight cuts to social services. </P>
<P>A win for 1707 is a win for all working-class New Yorkers. Every union in New York has a stake in this fight, especially the city's 125,000 municipal workers. Broad solidarity and support will be needed to back Bloomberg down and force him to look elsewhere in solving the city's budget crisis--such as taxing his corporate pals on Wall St.</P>

