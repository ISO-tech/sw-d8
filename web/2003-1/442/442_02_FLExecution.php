<P><font face="Times New Roman, Times, serif" size="4">Scheduled to be killed after 26 years</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Will Jeb Bush execute an innocent man?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 28, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ON FEBRUARY 26, Florida Gov. Jeb Bush wants to execute a man who has languished on death row for 26 years for a crime he says he didn't commit.</P>
<P>Last December, Amos King came within 15 minutes of being put to death when Bush issued a 30-day stay of execution so DNA tests could be conducted on new evidence. The tests came back inconclusive because the evidence is so old. But George W. Bush's baby brother set a new date for killing King anyway.</P>
<P>King's story underlines all the reasons why the death penalty should be abolished. King, who is Black and poor, was convicted of raping and killing an elderly white woman, Natalie Brady, in 1977.</P>
<P>At the time of the murder, King was living in a minimum-security prison. Prosecutors claimed that after King got off work from his job at a local restaurant, he broke into Brady's house, tortured, raped and stabbed Brady to death.</P>
<P>The coroner made up a story that Brady's rapist jabbed knitting needles into the victim's vagina--and the prosecution used this sickening detail to link the crime to King, using the testimony of a prison counselor who said he found King that night with blood on his shirt and pants.</P>
<P>King's clothes were examined, and no blood was found. And the prosecution never produced the pants at the trial. But they didn't need to. The horrifying story was enough for the all-white jurors--whose average age was 65 years old--to convict King. And eight years later, the coroner recanted the knitting needle story.</P>
<P>King's lawyer, a public defender, handed over his client's life on a silver platter. King was familiar with lawyer Thomas Cole's work--Cole was responsible for King's previous conviction on a gun theft charge. When King petitioned for another public defender, the court refused. Cole was so unprepared for King's capital trial that he himself asked to be taken off the case. But the judge refused that, too.</P>
<P>Anything that could have gone wrong for King did. But he has fought for 26 long years to expose the truth. Now Jeb Bush wants to bury the truth.</P>
<P>"The judicial system has failed," King wrote in a letter sent to <I>Socialist Worker</I>. "I have complained of ineffective counsel for over 25 years, even to the governor himself, knowing that an execution can be held on this evidence. I'm an innocent man."</P>


