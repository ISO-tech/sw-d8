<P><font face="Times New Roman, Times, serif" size="5"><b>Resisting the myth of American apathy</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 28, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>In response to Michael Newton's letter ("Do Americans Care About War?" <I>SW, </I>February 7), I'd like to say that one of the biggest myths in the U.S. is that ordinary people in this country are isolationist and don't care about anyone else.</P>
<P>The real story is that there is an effort in this society to keep working class and poor people in the dark. The media only tell one side of the story about conflicts in other places. For example, they completely cover up the true root of the conflict between Israel and Palestine--that Israel stole the land of Palestine.</P>
<P>And where are the "investigative journalists" who question the Bush administration's sources on their evidence against Iraq or the legitimacy of "regime change"? They are silenced by the corporate-run media.</P>
<P>When people know, they do care. My coworker, a Vietnam veteran, talks about what he did when he went AWOL from the U.S. military during the war--he went and lived in a Vietnamese village for nine months. He went into the war talking about the evils of "commies," and he came out saying, "I realized that I was the terrorist, killing innocent and lovely people."</P>
<P>If there is to be a mass antiwar movement, it's true that people will have to see how a war on Iraq will also be a war on ordinary people here at home. And people will see it for themselves, as hundreds of thousands of working-class soldiers are sent to Iraq. In the meantime, Washington will be cutting budgets, stripping civil rights and attacking our living standards at home.</P>
<B><P>Sarah Knopp, </B>Los Angeles</P>

