<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Can mass protest put an end to war?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 28, 2003 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ONE WEEK after the largest global protests against war ever, the warmongers are even more determined to lead the U.S. into war in Iraq. George W. Bush dismissed the mass protest as nothing more than a "focus group" that wouldn't alter his plans.</P>
<P>So many who rallied in more than 600 cities worldwide February 15-16 are asking themselves what they have to do to stop the war. If the huge protests have failed to dissuade the war makers, is there any hope that the mass of people can have an impact?</P>
<P>The decision to go to war is the most serious commitment a government can make. More often than not, populations must be dragged into supporting a war with government propaganda, outright lies and deception. And if it needs to, even the most "democratic" government will use coercion--like banning demonstrations, arresting antiwar organizers or enforcing conscription--to get its way.</P>
<P>Faced with government leaders determined to launch a war, our side should understand that no amount of "public opinion" will change their minds. However, this does not mean that protesting is useless. In fact, it's crucial to creating the political climate that can defeat the warmongers.</P>
<P>To many antiwar activists protesting in the mid-1960s, it seemed that the U.S. would escalate the war in Vietnam no matter what protesters did. But these early protests helped to crack the Cold War consensus that silenced any challenge to U.S. wars carried out in the name of fighting "communism." Later, as the movement became truly massive, fear of mass unrest caused the U.S. to abandon some of its most loopy plans in Vietnam--like Nixon's 1969 threat to use nuclear weapons.</P>
<P>Three key elements came together to end the war in Vietnam: a determined national liberation movement in Vietnam, a mass opposition to the war in the U.S. and antiwar sentiment and organizing inside the military itself. The last element was crucial. It was the product of years of antiwar activism involving veterans and even active-duty soldiers.</P>
<P>The stateside antiwar movement gave confidence to soldiers with doubts about Vietnam to become war opponents. "By every conceivable indicator, our army that now remains in Vietnam is in a state approaching collapse, with individual units avoiding or having refused combat, murdering their officers and non-commissioned officers, drug-ridden, and dispirited where not near-mutinous," wrote Marine Corps Col. Robert D. Heinl, Jr. in a famous 1971 <I>Armed Forces Journal</I> article. In many parts of Vietnam, ordinary U.S. soldiers made "peace" agreements with Vietnamese fighters.</P>
<P>The U.S. establishment--including Donald Rumsfeld, the defense secretary who presided over the final retreat from Vietnam--learned that its mighty weapons were useless if they could find no one willing to fire them.</P>
<P>Pacifist or moral objections to war didn't drive the soldiers' revolt so much as a realization among working-class GIs that the war was not being fought for their interests. In the same way, German soldiers--following the example of their Russian brothers and sisters of a year earlier--put an end to the First World War in 1918. </P>
<P>When right-wing naval officers in October 1918 ordered the German navy to make a last-ditch attack against the English fleet, sailors refused to move the ships. Word of the sailors' mutiny spread to other parts of the armed forces, and German workers staged mutinies and antiwar demonstrations. The mutiny soon became a revolution as workers and soldiers took over factories and cities--and overthrew the Kaiser.</P>
<P>Most people don't think of the German Revolution as an "antiwar movement." But it, along with the Russian Revolution of 1917, was one of a handful of instances when ordinary people not only forced a government to end a war, but also tried to build a new society to put an end to war itself.</P>


