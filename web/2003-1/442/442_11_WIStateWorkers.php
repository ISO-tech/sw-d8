<P><font face="Times New Roman, Times, serif" size="4">Republican legislature takes aim at state workers</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Showdown in Wisconsin</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mike Imbrogno, steward, AFSCME Local 171</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 28, 2003 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MADISON, Wis.--The state of Wisconsin is looking for a fight. On February 13, the Joint Committee on Employment Relations (JoCER) voted down a tentative agreement between the state's negotiating team and the unions that represent several groups of state workers. This could lead to a strike by state workers.</P>
<P>The rejected contract proposal contained paltry raises of 1 percent in the first year, 2 percent the second year, and an additional 2.5 percent for the final three months, but apparently even this was too much for the state to bear.</P>
<P>Now, 31,000 state workers represented by AFSCME Council 24, the American Federation of Teachers (AFT) and other unions face the prospect of spending even more time working without a contract. Our last contract expired on June 30, 2001! To us, this vote was just pouring salt in our wounds.</P>
<P>With the third lowest corporate tax rate in the country, Wisconsin faces a $3 to 4 billion budget shortfall. So what solution is being proposed by the Republican-controlled legislature? Balance the budget on the backs of already low-paid workers.</P>
<P>"Twenty-two years ago when I started working for the state, I was making a good wage," Mark Thomas, president of AFSCME Local 171, told <I>Socialist Worker</I>. "Now we have people working union jobs who qualify for public assistance."</P>
<P>During the 1990s, when there was a surplus, state workers received minimal increases in pay while inflation rose all around them. Now there are custodians trying to support families while making $8.68 an hour. This is criminal!</P>
<P>Unfortunately, this is the logical conclusion of labor-management "cooperation"--which these days appears to more and more union members as the union cooperating with management to keep wages down.</P>
<P>Marty Beil, the executive director of AFSCME Council 24, has said that under no circumstances will the contracts be renegotiated as the Republicans demand. This is an uncharacteristically hard stand from a man who is notorious for making sweetheart deals with former Gov. Tommy Thompson, who's now the Bush administration's Secretary of Health and Human Services.</P>
<P>The length of time without a contract and the stepped-up attacks by the legislature have forced workers to fight back. In the days leading up to the JoCER vote, workers organized rallies and informational pickets in their communities, work sites and at state offices in Madison.</P>
<P>Several clerical workers in some areas and upwards of 30 percent of first-shift prison guards participated in a "Blue Flu" campaign--calling in sick en masse. And hundreds of workers attended the JoCER meeting when the vote was taken.</P>
<P>In the week since the vote, many more workers have taken to the streets. Carrying signs saying "I make Wisconsin work," hundreds of state workers attended a spirited rally inside the Capitol building on February 18, demanding the legislature make good on the contracts that were bargained and agreed to in good faith.</P>
<P>Increasing numbers of workers are sensing the need to organize to fight for what we deserve. Meetings are being planned to pull together representatives from locals all over the state to coordinate actions and mobilize as many people as possible.</P>
<P>"Circumstances are going to force Council 24 to act like a union and organize its members to fight," said Thomas. "We are going to start organizing from scratch in some places, but at least we're starting. State employees have traditionally relied on political influence through elections and the like, instead of organizing their members. I hope those days are over. Heading into war and recession reminds me of the great organizing that went on in the tough times of the 1930s&#133;Those days could be coming back."</P>
<P>It won't be easy, but every state worker has to realize that these attacks won't just stop. We have to draw a line in the sand and let them know that we won't pay the price for their warped budget priorities. If it's a fight they want, it's a fight they'll get.</P>
