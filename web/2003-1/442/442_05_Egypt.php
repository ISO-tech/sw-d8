<P><font face="Arial, Helvetica, sans-serif" size="2"><b>EGYPT</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Government uses torture on detained antiwar activists</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Resisting Mubarak's crackdown</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mostafa Omar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 28, 2003 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>OPPONENTS OF a U.S. war on Iraq are defying two decades of martial law and a crackdown by the Egyptian government of President Hosni Mubarak. Several thousand people braved riot cops to demonstrate against the war drive and for political rights in Cairo on February 15, as part of the international day of antiwar action. Four days later, thousands of students at Al-Azhar University, the country's third largest, protested the war, clashing with police when they tried to march off campus.</P>
<P>Mubarak's government has always used heavy-handed repression against opponents. In the weeks leading up to February 15, authorities arrested and tortured 15 antiwar activists, including Ibrahim El-Sahari, a journalist and cofounder of the Center for Socialist Studies.</P>
<P>Pressure from human rights groups like Amnesty International and an international solidarity campaign pushed the government to release four of those arrested, including El-Sahari. At a press conference, El-Sahari described the torture used against the activists--including beatings and electric shock--and the inhumane conditions in Egyptian prisons where more than 30,000 political opponents of Mubarak are incarcerated.</P>
<P>Only days later, the authorities kidnapped Kamal Khalil, a longtime labor activist and president of the Center for Socialist Studies. Once again, Egyptian activists are calling for international solidarity to put pressure on the government to release Khalil.</P>
<P>Mubarak's government has criticized Bush's war drive against Iraq, as well as U.S. support for Israel's war on Palestinians during the two-year-old uprising, or Intifada. But these criticisms are seen by millions as empty gestures. </P>
<P>The antiwar protests are also fueled by anger over rising poverty and a jobless rate approaching 20 percent--the twin results of International Monetary Fund (IMF) policies of austerity and privatization that Mubarak has loyally implemented.</P>
<P>Since the 1970s, Egypt has been one of the U.S. government's top Arab allies in the Middle East--and the second-largest recipient of U.S. foreign aid after Israel. It was the first Arab government to sign a unilateral peace treaty with Israel in 1979, leaving the Palestinians to face Israel on their own. In 1991, Mubarak supported the U.S. led war on Iraq--and in exchange received $10 billion from Kuwait and other rich Persian Gulf states, as well more IMF loans.</P>
<P>But more and more people are questioning Egypt's relationship with the U.S.--and U.S. domination of the region. This renewed anti-imperialist consciousness is finding expression in the growing antiwar movement, which has already surpassed in size and depth the one that stood up to the 1991 Gulf War.</P>
<P>Activists in the U.S. need to stand in solidarity with opponents of the U.S. war around the world--especially in police states like Egypt that are propped up by Washington.</P>
