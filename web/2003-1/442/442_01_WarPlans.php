<P><font face="Times New Roman, Times, serif" size="4">Bush thumbs his nose at millions opposed to war</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>And he calls this a democracy?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 28, 2003 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"THE COUNTDOWN to war has begun." That's the meaning of the new United Nations (UN) Security Council resolution proposed by the U.S. and Britain, according to the <I>Financial Times</I>. "President George W. Bush and [British Prime Minister] Tony Blair--the axis of enforcement in the Security Council--both made clear at the weekend that Baghdad has, in effect, three weeks to give up its rogue weapons or face attack," the newspaper observed.</P>
<P>The BBC pointed out that since the resolution has no deadline, "the timetable would have left the Security Council and would be in the hands of the U.S. commander, Gen. Tommy Franks." So much for the 10 million people who demonstrated around the world February 15-16 against a U.S.-led war on Iraq--including up to 1 million in the U.S. alone, the biggest antiwar protests since the Vietnam War.</P>
<P>George Bush couldn't care less. "Size of protest, it's like deciding, well, I'm going to decide policy based upon a focus group," a smirking Bush announced to reporters. "The role of a leader is to decide policy based upon the security--in this case, the security of the people."</P>
<P>Translation: Bush claims the sole right to decide whether to launch a war on Iraq in the name of "democracy"--even when that means ignoring the massive opposition inside the U.S. and the overwhelming antiwar sentiment in countries with pro-war governments like Britain, Spain and Italy.</P>
<P>The plan for Iraq's "liberation" is spelled out in a Pentagon strategy called "shock and awe," which calls for 900 cruise missiles to be launched at Iraq in two days--more than twice the number used in the entire first Gulf War. Defense Department adviser Harlan Ullman, who co-authored the plan, compared the effect to "the nuclear weapons at Hiroshima, not taking days or weeks, but minutes."</P>
<P>Such an assault would inevitably kill tens of thousands of people--which is why Bush is already blaming Saddam Hussein for the atrocities that the U.S. is ready to commit. "Saddam Hussein is positioning his military forces within civilian populations in order to shield his military and blame coalition forces for civilian casualties that he has caused," Bush said February 12. </P>
<P>The reality is that Washington regards the slaughter of innocent Iraqis as a way to terrorize the world into following the U.S. line. During the Vietnam War, the Pentagon became infamous for the statement that U.S. troops had to destroy a village in order to save it--exposing the savagery of Washington's war on the Vietnamese.</P>
<P>Now Washington wants to destroy Iraq--or what's left of it after the 1991 Gulf War and 12 years of sanctions--in order to "liberate" it. We have to say no to the horror that the U.S. is prepared to unleash--and build the fight to stop these killers.</P>


