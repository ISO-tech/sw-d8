<P><font face="Times New Roman, Times, serif" size="5"><b>Defend civil liberties</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jonah Birch</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 28, 2003 | Page 10</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Activists gathered outside the Immigration and Naturalization Service (INS) office at Federal Plaza on February 21 to protest the "special registration" of immigrants.</P>
<P>Under a new provision of the USA PATRIOT Act, immigrants from certain countries are now required to register at the INS. Hundreds of immigrants waited outside in a line that stretched all the way around the building, while protesters chanted, "Open up the borders. Close the INS!" and "Immigrant rights are under attack. What do we do? Fight back, fight back!"</P>
<P>The special registrations, which target young men from 11 predominantly Muslim countries, as well as North Korea, are a part of a broader drive by the Bush administration and its congressional allies to do away with our most basic civil liberties.</P>
<P>But protesters refused to be intimidated. "Special registration is so wrong," said one young woman. "We're a nation of immigrants. The racism needs to stop."</P>


