<P><font face="Times New Roman, Times, serif" size="5"><b>"This is about imperialism"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 28, 2003 | Pages 6 and 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>SCOTT RITTER is a former Marine captain who served as the gung-ho chief of the United Nations Special Commission weapons inspectors in Iraq during the 1990s. Declaring himself the "alpha dog," Ritter--who made no secret of his close ties to both U.S. and Israeli intelligence--caused one of the major confrontations between inspectors and the Iraqi government.</P>
<P>He finally quit over what he called Washington's refusal to confront Iraq. Yet over the past five years, Ritter has become a different person. He exposed the U.S. government's infiltration of the UNSCOM for its own ends--and became a harsh and outspoken critic of Bush's plans for a new war.</P>
<P>At a student antiwar rally following the massive February 15 demonstration in New York City, Ritter ended his speech with these words: "No war with Iraq--not now, not ever, not in my name, not in your name, not in our name!" Before his speech, ERIC RUDER talked to Ritter about why he opposes the Bush gang's war drive.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<B><P>WHAT DID you think of Colin Powell's United Nations speech making the U.S. case for war?</P>
</B><P>I THINK it was masterful theater, but it lacked any meaningful substance. As an argument for war, it fell far short of the mark necessary to justify the expenditure of American life, or the taking of Iraqi life. No proof was offered, and I think Colin Powell disgraced himself as a man of moral character by allowing himself to be used in this fashion. </P>
<P>I can say--as an intelligence officer who spent many years on this, and specifically in regards to Iraq--that he presented extremely weak data. Photographs that show nothing. Transcripts that showed nothing. Diagrams from defectors of questionable veracity. </P>
<P>Colin Powell is a former general who has led men in combat and knows what it means to ask men to go out and die. He's got to take a long hard look in the mirror and decide whether or not that data would justify him giving an order for people to go over the top and lose their lives. The answer is, of course, no. So why did he allow himself to give that presentation? </P>
<P>It's humiliating. He's been shown to have presented false information, and yet, he seems to be unapologetic about it. But the world saw through it. I think you saw with the demonstrations here in New York and around the world that people have not bought into it. </P>
<P>Thankfully, there are enough people of strong moral character to fill the void that Colin Powell left when he stepped back and agreed to do that presentation.</P>
<B><P>YOU ADMIT that you voted for George W. Bush. What do you think of him now?</P>
</B><P>HIS ACTIONS don't match his rhetoric. This is a man who speaks of high moral values, a man who spoke of having a humble foreign policy, a man who spoke of being a uniter, not a divider. And he has basically failed to deliver on all fronts. </P>
<P>Not only that, he has implemented policies which are a direct affront to Americans from the standpoint of constitutionality--but also from a standpoint of being human beings. He's shown a total disregard for life--a willingness to take life for the most base of reasons, that of politics. </P>
<B><P>AS SOMEONE who was part of this war machine, what do you think the Bush war drive is really about?</P>
</B><P>THE BUSH administration consists of a clique of neo-conservatives who have carried out an effective coup d'etat, where they've seized decision-making power, both domestic and in foreign policy. In order to sustain their hold on that, they have to play on the age-old politics of fear and ignorance. They need perpetual combat and conflict. </P>
<P>We see that with the war on terror here at home. The U.S. homeland security is a reflection of this. And now abroad, they're seeking to implement the National Security Strategy document, which is that of American unilateralism--to use the overwhelming strength of the United States, militarily and economically, to impose our unilateral will on problems that we unilaterally define.</P>
<P>It's about hegemony. It's about imperialism. And Iraq is the case study for the implementation of this policy. Once they go into Iraq, they've set the precedent that will enable them to go wherever they want around the world. That's why it's so important that we draw the line right now with Iraq, and say no.</P>

