<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Is there a tide of anti-Americanism?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 28, 2003 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE WORLD is being swept by a tide of hatred and bigotry toward&#133;America. That's the line that Washington's war makers are pumping out as George W. Bush's planned slaughter in Iraq grows closer.</P>
<P>If you believe Dubya, of course, the U.S. is going to war against "terrorists" who are motivated by their hatred of America and "our freedoms." But "anti-Americanism" has become the excuse to dismiss any opposition to the U.S. war--in countries like France and Germany, of course, but even in the U.S. itself.</P>
<P>You don't have to look far to see that the bigotry actually runs the other way. In the U.S. media, the French are now casually dismissed as "cheese-eating surrender monkeys"--and that's not to mention the racist stereotypes about Arabs that have become perfectly acceptable.</P>
<P>Blowhard George Will had the gall to tell this "joke" in a <I>Newsweek</I> column: "How many Frenchmen does it take to defend Paris? No one knows, it's never been tried." What a clever joke--about a country where more than 6 million soldiers, three out of every four who served, were either killed or wounded in the First World War.</P>
<P>The tirades about anti-Americanism are the last refuge of scoundrels who can't justify their war on Iraq. George Will and friends complain about the "ingrates" who don't recognize all the good that the U.S. government does. But that's precisely what people across the globe question.</P>
<P>Since taking office, the Bush administration has not only declared its intention to go to war wherever it pleases against "terrorism," but it rejected the Kyoto Treaty on global warming, refused to recognize the new International Criminal Court, defended U.S. drug company patents despite Africa's AIDS epidemic and generally offended people everywhere with its incredible arrogance.  That's what "America" represents around the world, and people are right to be against it.</P>
<P>One motive for Washington's talk about "anti-Americanism" is to create a false "us vs. them" mentality--to make it seem like all people in the U.S. are in it together. But they're not.</P>
<P>The Bush gang and the corporate elite it serves stand to gain from the oil bonanza that will follow the war, as well as the more general aim of further asserting U.S. power. Ordinary people in the U.S. will be asked to fight the war and sacrifice their living standards--while gaining nothing from the imposition of Washington's power.</P>
<P>There are actually two Americas, one made up of the haves and the other the have-nots. And there is a brand of anti-Americanism--one that in Europe often gets a radical tinge--which doesn't make this distinction.</P>
<P>It comes from seeing public support inside the U.S. for imperialist war--even the bare majority in the case of Iraq--as evidence that all of America must be a bastion of conservatism and ignorance. For example, one British film producer told a <I>San Francisco Chronicle</I> reporter that she thought ordinary Americans were to blame for their "failure to understand the Iraq situation as well as they should. They complain about 'Old Europe's' reaction, but I feel that we are much more aware and informed than they are."</P>
<P>There is some truth in this comment. Ordinary people in the U.S. are systematically miseducated about events and conflicts in the world, and because of this, they often accept Washington's chauvinism. But any view of America that accepts this as unchanging is wrong.</P>
<P>In greater or lesser numbers, ordinary people do see through Washington's lies and take action to oppose their own government. The mass movement against the Vietnam War was one example--and another is today's growing struggle against the war on Iraq.</P>
<P>The worldwide protests against the war on February 15 showed an internationalist alternative to the politicians' hysteria about "anti-Americanism"--and to the mistaken belief that all Americans always support the actions carried out in their name by the U.S. government.</P>

