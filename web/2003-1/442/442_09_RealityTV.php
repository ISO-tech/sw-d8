<P><font face="Times New Roman, Times, serif" size="4">Profiting off sensationalism and sexism</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Reality TV cash machine</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Amy Muldoon</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 28, 2003 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MILLIONS OF viewers tuned in last week to Fox's <I>Joe Millionaire</I> to find out who fake millionaire Evan would choose to live out his TV fairy tale. The show--in which women compete for the affections of Evan, a construction worker posing as a man of wealth--was pitched as the ultimate con that asked the question "Are women just competitive gold diggers?"</P>
<P>Like most reality TV, the premise played on sexist stereotypes and raw competition for cash to draw its audience. Like many reality shows, it was immensely popular.</P>
<P>Despite the vast resources of the production, the manipulation and editing that hundreds of hours of tape went through, in the end, a strange message came through "Joe"--that working-class men don't really enjoy lying to women and don't chose their mates purely based on who looks best in a tiny bikini. In fact, Evan chose substitute teacher/elder care worker Zorah, who said she was "turned off by the inheritance."</P>
<P>This cannot have been the moral the producers hoped for. However, it's hard to imagine, after calculating their profits, that they really cared what message came through.</P>
<P>Reality TV is an enormous cash cow for networks and producers in particular, because they've finally found a way to get around the Screen Actors and Writers Guilds, the unions that negotiate for actors and script writers.</P>
<P>In fact, the explosion of reality TV programming came in 2001, during the buildup to a strike by both unions. Even when the strike was averted, networks held on to their reality show spots because, unlike scripted shows, reality shows make money immediately--not over years through royalties on repeats.</P>
<P>Not only do reality shows free networks from salaries or benefits to casts and writers, they avoid all the other unionized support outfits as well--musicians, scene builders and film crews. An anonymous program executive told the <I>New York Times,</I> "Writers are petrified. This could change the whole economics of this town."</P>
<P>Also, competition from cable and TiVo have pushed the networks to look for new ways to lure in viewers and advertisers. With people taping shows and skipping commercials, networks have looked for ways to incorporate ads into the show itself.</P>
<P>The pioneer of this tactic is <I>American Idol,</I> which has Ford Motors and Coca-Cola as its primary sponsors. Not only do their logos appear all over the set when contestants perform, they insert plugs wherever they can. In one episode, a contestant ran from his audition to the hospital where his wife was giving birth. The cameras zoom in on him holding his new baby--which is labeled a "Coca-Cola moment."</P>
<P>Reality shows are interesting because they present more extreme versions of everyday life: competition, cooperation, embarrassment and even fear. Probably the oldest reality show on the air, MTV's <I>Real World</I>, is simply about how difficult it is to get to know and trust people that you're randomly thrown together with.</P>
<P>Of course, reality shows are still products of mainstream, for-profit corporate media, so for every minute of Zorah saying she would sell her gifts of jewelry to pay for her aunt's surgery, or someone on the <I>Real World</I> coming out of the closet, there is 10 minutes of hot-tub cat-fighting.</P>
<P>Sexism is the bread and butter of reality TV. Fox's next project, <I>Bridezillas, </I>about nine normal women turning into monsters planning their weddings, and ABC's upcoming <I>Wife Swap,</I> in which families will trade mothers, are just a taste of what they have in store. Not to mention <I>Are You Hot?</I>--an entire show devoted to swimsuit competition, complete with a judge who wields a laser pointer over contestants' competing body parts.</P>
<P>Competition among networks will drive reality shows into ever more unreal locations and stunts--just as competing with Jerry Springer has caused every daytime talk show to have strippers and out-of-control teens.</P>
<P>But there will be surprises along the way, for both viewers and producers, like an open homosexual winning <I>Survivor</I>. Or there are the six housemates in <I>Big Brother,</I> who staged a walkout rather than sell each other out for $1 million--which the network upped to $50 million (they still refused).</P>
<P>And the all-too-real reaction of Zorah when the happy couple was surprised with a joint check for a $1 million--a statement which many of the 15 million viewers could relate to: "Oh my God. All I have is $189 in the bank!"</P>


