<P><font face="Times New Roman, Times, serif" size="5"><b>Our small victory against the death penalty</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 28, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Like folks all over the country, I was overjoyed by Illinois Gov. George Ryan's decision to empty death row. I have worked for a long time with the Campaign to End the Death Penalty in Texas. Our system here is as rotten as in Illinois or anywhere--and after we stopped celebrating the Illinois victory, we got down to work on our own campaigns and cases with renewed energy.</P>
<P>We had our own small victory in Austin recently. One of the cases that we have been working on is the "Yogurt Shop" case. Four girls were killed several years ago in an Austin-area yogurt shop. The crime went unsolved for many years, and the nature of the crime and scrutiny from the media put pressure on police to make an arrest.</P>
<P>Despite the lack of physical evidence, police and prosecutors eventually targeted four young men--Forest Wellborn, Maurice Pierce, Michael Scott and Robert Springsteen. These men were among hundreds of folks that they had questioned in their early investigation.</P>
<P>Through well-documented coercion (including holding a gun to Mike Scott's head during questioning), police obtained confessions from Robert and Mike. Forest was released for lack of evidence. </P>
<P>With only a false confession, they sent Robert to death row. They tried Mike, and he was given a life sentence. The last man to be tried was Maurice Pierce. Maurice didn't confess and maintained his innocence throughout. There was no physical evidence linking him or the others to the crime. </P>
<P>Finally, just a few weeks ago, after three years behind bars waiting for trial, Maurice was let out of jail, and the charges against him were dropped.</P>
<P>Last fall, during the trial of Mike Scott, the Campaign held pickets at the courthouse and organized press conferences and public meetings. When Mike was given a life sentence, we were glad only that it wasn't the death penalty. </P>
<P>It's our belief that the work we did may have impacted the district attorney's decision not to try Maurice, and we hope this new development will help in the appeals process for the others. </P>
<P>I'd like to end by saying thank you to the <I>SW</I> for the wonderful and moving coverage of the Illinois victory. This is the biggest victory against the death penalty since it was temporarily abolished in 1973. I hope this is just the beginning!</P>
<B><P>Lily Hughes,</B> Austin, Texas</P>

