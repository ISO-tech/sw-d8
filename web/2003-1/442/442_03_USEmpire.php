<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Washington's war aims extend beyond the invasion of Iraq</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The war for U.S. empire</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 28, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE BUSH calls Saddam Hussein "a brutal dictator, with a history of reckless aggression, with ties to terrorism, with great potential wealth." His solution? Install a new dictatorship--made in the USA.</P>
<P>"The Bush administration plans to take complete, unilateral control of a post-Saddam Hussein Iraq, with an interim administration headed by a yet-to-be-named American civilian who would direct the reconstruction of the country and the creation of a 'representative' Iraqi government, according to a now-finalized blueprint described by U.S. officials and other sources," the <I>Washington Post</I> reported. "Gen. Tommy Franks, the head of the U.S. Central Command, is to maintain military control as long as U.S. troops are there"--a period expected to be years.</P>
<P>Much has been made--rightly--about the statements of U.S. officials that the occupation of Iraq would be paid for by revenue from that country's enormous oil reserves. But while oil is a major prize in Iraq, U.S. war aims are also strategic.</P>
<P>The U.S. invasion of Iraq--a shattered and defenseless nation--is also designed to back up a point made in Bush's National Security Strategy document published last summer. "Our forces will be strong enough to dissuade potential adversaries from pursuing a military build-up in hopes of surpassing, or equaling, the power of the United States," the document said.</P>
<P>And the greater the horrors of a U.S. war in Iraq, goes the administration's thinking, the more likely that Europe, Russia, China, Japan and other countries will be "dissuaded" from challenging Washington. The Bush administration's war on Iraq isn't only for oil, but for empire.</P>
<P>This drive by Washington to impose its will on the world has provoked a backlash in France, Germany and Russia, which are putting up diplomatic resistance to the U.S. Even U.S. diplomats are reporting Washington's isolation, according to a <I>Washington Post</I> story earlier this week. "The messages from U.S. embassies around the globe have become urgent and disturbing: Many people in the world increasingly think President Bush is a greater threat to world peace than Iraqi President Saddam Hussein," the <I>Post</I> reported.</P>
<P>The Bush administration's solution has been to construct an alliance in support of the war through bullying and bribery--like the $30 billion in aid and loans over three years for Turkey in exchange for the government's permission to invade Iraq from its territory.</P>
<P>"Small countries that currently have seats on the [United Nations] Security Council have suddenly received favorable treatment for aid requests, in an obvious attempt to influence their votes," <I>New York Times</I> columnist Paul Krugman wrote. "Cynics say that the 'coalition of the willing' President Bush spoke of turns out to be a 'coalition of the bought off' instead."</P>
<P>Meanwhile, Bush expanded the scope of the "war on terrorism" last week when he dispatched 3,000 combat troops to the Philippines under the guise of fighting terrorism--although the real aim of the intervention is to reestablish links to that country's military after popular pressure there forced the U.S. to close bases in the 1980s.</P>
<P>At the same time, Bush used his authority to exceed limits imposed by Congress on U.S. troops in Colombia, sending 150 more "advisers" following the rebel capture of U.S. civilian contractors accused of being CIA agents.</P>
<P>That's why we need to follow up the tremendous protests in New York, San Francisco and other cities with more organizing. We need to build a movement that can reach out to all the people in every corner of society who are questioning Bush's war drive--and turn their doubts into action.</P>
<P>We need to mobilize for the March 5 day of actions, spread the word about emergency mobilizations set for the day after a U.S. attack on Iraq and prepare for future mobilizations, like the recently called April 5 national demonstrations in Washington, D.C., San Francisco and other cities.</P>
<P>The Bush gang has started the countdown to a slaughter in Iraq. Taking a stand against this war for oil and empire has never been more urgent and important.</P>

