<P><font face="Times New Roman, Times, serif" size="5"><b>Bush gang blocks cheap drug plan</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 28, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH administration is making it harder for poor people around the globe to stay alive. At a World Trade Organization (WTO) conference in Switzerland last week, U.S. Trade Representative Robert Zoellick blocked a proposal that would have loosened global patent rules on drugs and allowed poor countries to import and manufacture generic, low-cost versions of life-saving medicines.</P>
<P>In 2001, WTO representatives agreed to a plan for poor countries to be able to override patent rules and license local companies to copy essential drugs when public health is at stake. But they failed to specify how countries without the capacity to manufacture generics could gain access.</P>
<P>Zoellick offered a proposal that would limit drug imports to a small list of 15 diseases, including HIV/AIDS, malaria and tuberculosis. Illnesses like pneumonia, which will take the lives of 3 million kids in Africa this year, didn't make the list.</P>
<P>The U.S. scheme would also make countries wait until they could show that they were in a "national emergency" to qualify--leaving untold thousands to die in the meantime.</P>

