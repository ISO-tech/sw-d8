<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The new "white man's burden"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 7, 2003 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. has always projected its own narrow economic and military interests around the world as in the broad interests of everyone on the planet. "American principles," said Woodrow Wilson in 1917, "are the principles of mankind and must prevail."</P>
<P>In 1907, Wilson explained these sacred principles: "Since trade ignores national boundaries, and the manufacturer insists on having the world as a market, the flag of his nation must follow him, and the doors of the nations which are closed against him must be battered down. Concessions obtained by financiers must be safeguarded by ministers of state, even if the sovereignty of unwilling nations be outraged in the process."</P>
<P>In his State of the Union address, Bush took imperial arrogance to a new level, asserting that U.S. interests are God's interests: "America is a strong nation and honorable in the use of our strength," he sermonized. "We exercise power without conquest and sacrifice for the liberty of strangers."</P>
<P>"The liberty we prize," he intoned, "is not America's gift to the world, it is God's gift to humanity." This from a man who is planning a brutal invasion and military occupation of Iraq. </P>
<P>This nauseating rhetoric has always meant something different in practice. The U.S. can't state its real aims: to show that any nation that defies it will be smashed pitilessly--especially those sitting on 10 percent of the world's proven oil reserves.</P>
<P>But as the world's sole superpower, it wants friends and allies alike to understand that it alone decides what nations and what rulers are playing by the proper rules (which it, of course, establishes), and who shall be punished and rewarded. </P>
<P>This imperial policing is always portrayed as some kind of odious task, a burden that the U.S. is forced to accept, as a nation uniquely adapted for the job. "The emerging global order needs an enforcer," wrote <I>New York Times</I> columnist Thomas Friedman a few years ago. "That's America's burden." This was called the "White Man's Burden" at the turn of the last century.</P>
<P>Notwithstanding its rhetoric about freedom and democracy, the U.S. has shown a penchant for establishing strong dictatorial regimes to defend its interests overseas.</P>
<P>The statement of Henry Kissinger on the electoral victory of socialist Salvador Allende in Chile is emblematic: "I don't see why we need to stand by and watch a country go communist due to the irresponsibility of its people. The issues are much too important for the Chilean voters to be left to decide for themselves." Accordingly, the U.S. gave aid and support to the Chilean military in order to remove Allende from power, which it did in 1973.</P>
<P>The U.S. in fact prefers dictators. Writing in the January-February issue of <I>Foreign Affairs,</I> Johns Hopkins professor Fouad Ajami, in an article strenuously promoting the coming attack on Iraq, admits, "Few Arabs would believe this effort to be a Wilsonian campaign to spread the reign of liberty in the Arab world. They are to be forgiven their doubts, for American power, either by design or by default, has been built on relationships with military rulers and monarchs without popular mandates."</P>
<P>Middle East specialist Dilip Hiro spelled out the reason for this: "It is much simpler to manipulate a few ruling families (and to secure fat orders for arms and ensure that oil prices remain low) than a wide variety of personalities and policies bound to be thrown up by a democratic system."</P>
<P>In Iraq, the U.S. does not want democracy. One U.S. official was refreshingly candid about this after the first Gulf War, when the U.S. decided it preferred Saddam Hussein in power over an uprising from below. "Our policy," stated Richard Haass, then-director for Near East affairs on the U.S. National Security Council, "is to get rid of Saddam, not his regime."</P>


