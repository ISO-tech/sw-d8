<P><font face="Times New Roman, Times, serif" size="4">Trusteeship aims to head off rank-and-file revolt</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>AFT takes over D.C. teachers union</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jeff Bale, WTU member</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 7, 2003 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WASHINGTON--The Washington Teachers Union (WTU) was taken over by its parent union last month following a scandal in which former leaders stole at least $5 million in union funds. While at a conference in sunny Florida, leaders of the American Federation of Teachers (AFT) voted to put the WTU under administration, installing the union's former Northeast director, George Springer, as trustee.</P>
<P>Springer and the AFT made their intentions clear--not just to "clean up" the union, but to ensure that the membership continues to be kept in the dark, just as it was under the former leadership. Springer's first move was to cancel the monthly membership meeting scheduled for January 30.</P>
<P>He claimed that since it was only his first day on the job, he would need more time to become familiar with the situation. But Springer and the AFT apparently were informed enough to hold a press conference that same day to announce the trusteeship.</P>
<P>Members were outraged that our meeting was cancelled. A few challenged Springer at the press conference, others demonstrated that afternoon in front of AFT offices, and still more went ahead with the membership meeting to make future plans to take back our local.</P>
<P>Springer announced he would meet with building representatives February 3. Union activists are organizing to get as many WTU members there as possible. Whatever he has to say, he can say it to all of us.</P>
<P>Placing the WTU under administration has upped the ante for the future of our local. The AFT made this move because they saw an angry rank and file beginning to organize and take matters into its own hands with rallies, press events and even a special membership meeting, where more than 200 teachers took a vote of no confidence in the leadership.</P>
<P>The AFT had hoped that they could rally around Esther Hankerson, the former vice president, but that plan failed. So they're throwing down the glove to WTU members to see if we have the wherewithal to keep organizing.</P>
<P>The reality is, however, that WTU members are just beginning to relearn the basic methods of building and maintaining an active union membership. Union activists need to remember that there are no short cuts to reinvigorating a union membership that has been robbed, duped and led into passivity for years while bearing the brunt of crumbling schools.</P>
<P>We may not win every battle in taking back our union, but it is imperative that we organize around issues that can draw ever-increasing numbers of teachers into activity. A strong turnout at the February 3 meeting, followed by getting teachers to the February 18 court hearing, are the important first steps.</P>


