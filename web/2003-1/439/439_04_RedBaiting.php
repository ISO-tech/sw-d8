<P><font face="Times New Roman, Times, serif" size="5"><b>No place for red-baiting in our movement</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 7, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Some socialist groups in the antiwar movement are being smeared--both by people within the movement and commentators outside of it. This is red-baiting--and is often dressed up in the (incorrect) argument that socialist politics will scare people away from the antiwar movement.</P>
<P>Recently on the National Public Radio show "Fresh Air," Vietnam War-era activist Todd Gitlin blasted the antiwar organization International ANSWER. Gitlin called the politics of the group, and the Workers World Party behind it, "freakish"--citing spokesperson Ramsey Clark's support for Slobodan Milosevic and criticizing the top-down organizing structure.</P>
<P>In the same breath, Gitlin asserted that support for political prisoner Mumia Abu-Jamal and solidarity with Palestinians--campaigns that the Workers World Party is active in--are political issues that "have no place in the antiwar movement."</P>
<P>So much for "democracy." Apparently Gitlin feels he has the authority to declare what issues do and don't belong in the movement. His argument is that any politics beyond mainstream liberalism aren't welcome in the antiwar movement.</P>
<P>Antiwar activists should argue against the Workers World Party's sympathy for Stalinist regimes, as well the top-down structure of ANSWER. But we need to defend groups from attacks that are made just because they're socialists.</P>
<P>Arguing for a broad antiwar movement that includes many points of view will benefit the movement--making it a democratic one characterized by healthy discussion and debate. This is the only kind of movement that will be able to take on Bush.</P>
<B><P>Khury Petersen-Smith,</B> Rochester, N.Y.</P>

