<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISRAEL/PALESTINE</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>What will Sharon's victory mean?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 7, 2003 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ISRAELI PRIME Minister Ariel Sharon handed a crushing defeat to his rival Labor Party in last week's elections. Sharon's right-wing Likud Party doubled its representation in the 120-seat parliament to 38 seats--while Labor representatives fell from 25 to 19, the party's worst result ever.</P>
<P>With Likud's solid victory, Sharon--in keeping with his long career of war crimes--could now pursue his wildest fantasies, including "striking the Palestinians to [his] heart's content and exiling or killing Arafat," according to Israel's <I>Yediot Aharonot</I> newspaper. So far, though, Sharon has extended his hand to the Labor Party in the hopes of forming a unity government.</P>
<P>Why would Sharon try to woo Labor into a coalition government--even though Labor Party leader Amram Mitzna pledged for weeks that his party would not be part of a government headed by Sharon? Mainly because Sharon is worried about Israel's relationship with Washington.</P>
<P>The Bush administration, while a loyal supporter of Israel's right wing, doesn't want anything to threaten its war on Iraq--like another all-out assault on Palestinians in the Occupied Territories, which could turn right-wing Arab governments against cooperation with the U.S.</P>
<P>But with a period of six weeks to form his government, Sharon figures that time is on his side. If the war on Iraq begins within that window, he would likely be able to get Mitzna to join the government anyway--saving Likud from the uncomfortable prospect of having to implement its program without political cover.</P>
<P>"The great victor is not anxious to be left alone with his own political species," wrote <I>Ha'aretz</I> columnist Yoel Marcus. "He needs a brake; he needs a fig leaf; he needs a partner to blame his bungles and blunders on."</P>
<P>Throughout his time in office, Sharon has ratcheted up the level of violence against Palestinians in order to head off the need for real negotiations or concessions from Israel. "He's not really negotiating," says Asher Arian, a political scientist at Haifa University. "He's not moving. He's building the settlements."</P>
<P>And the Labor Party--far from posing an alternative to Sharon's dirty colonial war against Palestinians--gave Sharon its stamp of approval as a member of his government until just a few months ago.</P>
<P>The media portrayed Mitzna as a figure whose dovish views rivaled in their intensity the hawkish views of Sharon. But the truth is that Mitzna's political co-thinkers have taken every chance they had for three months to declare that there was no "peace partner" on the Palestinian side--echoing the views of Sharon and the far right. "It is no wonder Amram Mitzna was having a hard time mobilizing supporters for renewing negotiations with Yasser Arafat," writes <I>Ha'aretz</I> columnist Akiva Eldar.</P>
<P>Whether Likud or Labor controls Israel's government, the fact remains that Israel has been built on land stolen from the Palestinians. The only real solution to the conflict is a single, democratic state in all of Palestine that grants equal rights to Arabs and Jews.</P>


