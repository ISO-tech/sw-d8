<P><font face="Times New Roman, Times, serif" size="4">Neutron Jack's new bomb used on NYC education</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Budget ax hits our schools</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 7, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P></B>
<P>New York Department of Education Chancellor Joel Klein says that he wants to encourage "cooperative leadership" with teachers in the public schools--as he told the United Federation of Teachers' delegate assembly recently. But actions speak louder than words.</P>
<P>He has hired former General Electric executive Jack Welch to head up his training program for principals. Known as "Neutron" Jack because of his layoffs and union busting at GE, which, like a neutron bomb, left buildings standing without any people in them, Welch insisted that managers lay off the bottom 10 percent of their workforce.</P>
<P>New York City Mayor Michael Bloomberg has already begun following in Welch's footsteps. Last week, he announced a major restructuring, eliminating dozens of community school district offices.</P>
<P>Although some administrators will be fired, the mayor will also end up laying off unionized clerical workers--and then putting that administrative workload onto individual school secretaries who are already drowning in paperwork. Already, 380 union workers at the central office have been given pink slips.</P>
<P>Bloomberg and Klein also announced sweeping curriculum reforms which amount to nothing more than a $34 million boondoggle for textbook giant Prentice Hall.</P>
<P>Politicians have always blamed individual teachers and used education reform as a front to look like they care about working-class children. But the educational crisis in our society can't be resolved by more layoffs or by shuffling around the tiny amount of money currently spent on it. Education will only be fixed if there is a massive redistribution of resources away from the rich and the bloated military budget towards our schools. </P>
<P>Teachers and other city workers should follow the example of the 17,000 GE workers that struck against Jack Welch's company to fight for more spending on education, rather than layoffs.</P>
<B><P>Peter Lamphere,</B> New York City</P>

