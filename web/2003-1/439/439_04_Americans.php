<P><font face="Times New Roman, Times, serif" size="5"><b>Do Americans care about war?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 7, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>While I heartily agree with the article "Why you should oppose Bush's war on Iraq," (<I>SW,</I> January 10), most of the article contained--while perfectly valid points--nothing about the impact of war on the lives of Americans. </P>
<P>As Ambrose Bierce said, "War is God's way of teaching Americans geography." Sadly, war does seem to have that effect--because most Americans are ignorant and do not care about the rest of the world. </P>
<P>In the Vietnam era, Americans saw U.S. troops being killed, and the war stopped because of self-serving interest. Most Americans did not sympathize with Students for a Democratic Society or other organizations talking about the atrocities that we were performing. We saw the gruesome effects and made it applicable to us. The Pentagon will make sure that doesn't happen again.</P>
<P>In another article in this same issue of <I>SW,</I> the author says, "As if activists who want to stop a one-sided slaughter in Iraq--or end the sanctions that have killed more than 1 million Iraqis in the last decade--have something to apologize for!" </P>
<P>But we do have something to apologize for: we have not done our best to really integrate the ideology of American self-interest into our campaign against war. And for that, we may fail.</P>
<B><P>Michael Newton,</B> Sacramento, Calif.</P>

