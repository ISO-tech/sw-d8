<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Washington's campaign of lies to justify attack on Iraq</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>They want war no matter what</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 7, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>DO THEY even care whether we believe this stuff? In the week after the State of the Union address, the Bush gang was spouting a whole pack of lies--both new fibs and recycled whoppers--to back up their case for a war on Iraq.</P>
<P>As Secretary of State Colin Powell, the administration's alleged "dove," was wheeled out to twist arms at the United Nations (UN), the administration was sending its message, loud and clear: We want a war on Iraq, and nobody better stand in our way.</P>
<P>Bush vowed in his State of the Union speech that Powell would provide the hard evidence against Iraq when he speaks at the UN February 5. But according to the <I>Wall Street Journal,</I> Powell will have nothing new to say--only the same old U.S. claims about weapons programs that Iraq is supposedly trying to hide from UN inspectors.</P>
<P>Bush also said that the White House had evidence of direct ties between the Iraqi government and the September 11 attacks carried out by Osama bin Laden's al-Qaeda network. But the <I>New York Times</I> reports that even the CIA and FBI don't believe this. "We've been looking at this hard for more than a year, and you know what--we just don't think [the link is] there," one FBI official told the <I>Times</I>.</P>
<P>Then there's the most outrageous lie yet. The new Homeland Security Department leaked a report to the press last week claiming, among other things, that Iraq sent agents from Canada to New York and Washington to "intensify spying activities and to carry out anti-U.S. demonstrations to stop a war against Iraq." In other words, the hundreds of thousands of us who marched against war on January 18 were mobilized by "Iraqi spies."</P>
<P>With the "case" for war this obviously fake, why did support for a U.S. attack on Iraq go up slightly last week, according to opinion polls? The main reason is almost no one in mainstream Washington is countering Bush's lies.</P>
<P>You'd think that the Democratic Party would recognize the massive protests on January 18 and Bush's steady fall in his approval ratings as signs that they could gain support by opposing the war drive. But you'd be wrong&#133;again.</P>
<P>Most of the party's national leaders are only too happy to ride along on the White House war wagon. Sen. Ted Kennedy's (D-Mass.) call for a new vote on the congressional resolution to support Bush's war is being met with scorn by most of the do-nothing Democrats.</P>
<P>And of the four early candidates for the 2004 Democratic presidential nomination serving in Congress, three of them--Sens. John Edwards (D-N.C.) and Joe Lieberman (D-Conn.), and Rep. Richard Gephardt (D-Mo.)--say that they would support a war on Iraq, even if the UN opposed it.</P>
<P>Meanwhile, no one should expect the lapdog corporate media to expose the White House. They've gone along with the Pentagon's lies again and again. In the lead-up to the first Gulf War on Iraq in 1991, for example, the media never questioned the horrific stories they hyped about Kuwaiti babies being thrown out of incubators by invading Iraqi soldiers. Only after the war were the stories exposed as the fabrication of a public relations firm working for the Kuwaiti government.</P>
<P>The Bush administration will keep making up lies to justify its war--and the rest of official Washington will go along. Our movement has to expose Washington's war makers--and seize every opportunity to reach out to those who question the drive to a new war.</P>
<P>Antiwar organizations around the world have called for protests on the weekend of February 15. In the U.S., activists are building for a march in New York City that could bring out larger numbers than ever before. We can send Washington a clear message: We say no to your war!</P>


