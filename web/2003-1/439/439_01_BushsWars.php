<P><font face="Times New Roman, Times, serif" size="4">Iraq...abortion rights...civil rights...Medicare...unions</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Stop Bush's wars</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 7, 2003 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH wants to take us all back to the "good old days." Back to a time before affirmative action, when only rich white boys like him could go to college. When women had to risk their lives to end unwanted pregnancies in back-alley abortion offices. And when the federal government had no responsibility to help the unemployed, the poor, the disabled, the elderly.</P>
<P>Bush and his pack of mad-dog Republicans are off the leash--and they're attacking everything in their path, from civil liberties to union rights. But while the Bush administration plans to slice, dice and privatize Medicare and Social Security, it has billions to burn on the Pentagon war machine. Because there's plenty of money for the bloody massacre that Bush has planned for Iraq--despite the fact that he and his hawks can't come up with any solid evidence for why the U.S. should attack.</P>
<P>The assault is so broad that it should come as no surprise that opinion polls are showing greater dissatisfaction with Bush's policies--from his handling of the economy to his stance on affirmative action.</P>
<P>But not everyone is unhappy with Bush's ugly agenda. After his State of the Union speech last week, Peggy Noonan--the speechwriter for Bush's daddy who coined such phrases as "a thousand points of light" and "Read my lips: No new taxes"--oozed nothing but praise for Junior. "A steady hand on the helm in high seas, a knowledge of where we must go and why, a resolve to achieve safe harbor," Noonan wrote in the <I>Wall Street Journal</I>. "More and more, this presidency is feeling like a gift."</P>
<P>It's a gift all right--for the pirates on Wall Street, the bigots of the Christian Right and the chicken hawks who are thirsty for blood in Iraq. Bush is waging an all-out war on many fronts--not only on the Iraqi people halfway around the globe, but on working people here at home.</P>
<P>So we have a message for the "president." We've had enough of your attacks on our rights and enough of your war on Iraq. And we're standing up to say "No more."</P>


