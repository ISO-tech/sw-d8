<P><font face="Times New Roman, Times, serif" size="4">Dismantling restrictions on polluters</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>A war on the environment</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 7, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THOUGH ITS assault on Iraq has grabbed the headlines, the Bush administration has been quietly carrying out a new assault on the environment.</P>
<P>Many of the measures that would open up more of the environment to damage and destruction are slipping below the radar screen--because instead of being formal policy changes, White House officials have labeled them "reinterpretations" of existing laws.</P>
<P>Thus, in January, the Environmental Protection Agency (EPA) announced that it was "reviewing" its wetlands protection policy--and might decide to exclude more than half of the U.S. streams and marshes that were previously protected habitats.</P>
<P>And on January 17, the administration proposed opening up the largest remaining block of unprotected public land in the U.S. to oil and gas development. The 9 million acres of the Alaska North Slope were set aside in the 1920s for possible future energy development and have remained largely untouched since. But not if the Texas oil boys in the Bush administration get their way.</P>
<P>Environmental groups fear that North Slope oil exploration will be a stepping stone to the nearby Arctic National Wildlife Refuge, which the Bush administration may try again this year to open up to oil drilling.</P>
<P>Another change under consideration is a blanket exemption from environmental laws for the U.S. military--which would exempt every military base in the country from pollution standards. </P>
<P>A recent survey by Knight Ridder newspapers dug up more than 50 of these administrative "rewrites" in the first two years of the Bush administration alone. But none of this is surprising, considering the fact that oil, energy and logging companies all made major campaign contributions to George W. Bush when he was running for president.</P>
<P>"No matter how much the oil companies get, they keep coming back for more," Deirdre McDonnell, of Earthjustice Juneau in Alaska, told the <I>New York Times</I>. "And the Bush administration is only too happy to oblige."</P>


