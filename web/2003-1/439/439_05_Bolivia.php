<P><font face="Arial, Helvetica, sans-serif" size="2"><b>BOLIVIA</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">The struggle against Washington's anti-coca program rallies resistance</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Mass protests paralyze Bolivia</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Tom Lewis</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 7, 2003 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ROADBLOCKS AND mass protests against the government of President Gonzalo S&aacute;nchez de Losada paralyzed Bolivia in the second half of January.</P>
<P>Demonstrators demanding a halt to the government's program of eradicating coca plants succeeded in disrupting traffic along Bolivia's main highway, connecting the Andean city of Cochabamba with the tropical capital of Santa Cruz.</P>
<P>At one point, the army retaliated by completely surrounding Cochabamba--before assaulting the blockades. Twelve died and hundreds were arrested in violent clashes with police between January 13 and 27.</P>
<P>As <I>Socialist Worker</I> went to press, a fragile truce still held between the government and protest leaders. Rank-and-file meetings of the coca growers' union were to decide in the first week of February whether to renew anti-government actions.</P>
<P>The coca eradication program, a project of both the Bolivian government and the U.S. military's Andean Regional Initiative, is a centerpiece of corporate globalization in Bolivia. The aim is to create a stable business environment for U.S. capitalism and eliminate a possible source of funding for rebel movements.</P>
<P>An extension of the Plan Colombia anti-coca program further north, eradication in Bolivia has ruined the lives of thousands of small farmers. "The government doesn't want to discuss the coca issue because it has no viable solutions," said Evo Morales, a member of Congress from the Movimiento al Socialismo party (MAS) and the main leader of the coca growers (or cocaleros).</P>
<P>Many cocaleros are ready to give up coca production in exchange for equal- or better-paying jobs. But neither the government nor Washington has put any serious offers on the table. Until a real alternative is found, the cocaleros are demanding an indefinite pause in eradication.</P>
<P>The struggle of the cocaleros has become a rallying point for Bolivia's social movements. Images of the coca leaf now symbolize the plight of millions of Bolivian workers and peasants, who have suffered because of privatization of the economy during the 1990s.</P>
<P>This struggle has cast light on other battles as well--opposition to Washington's Free Trade Area of the Americas scheme to impose NAFTA-like policies throughout the Western Hemisphere, and anger at the sale of Bolivian natural gas to the U.S.</P>
<P>The cocaleros have received active support from peasant organizations, the main state and local factory workers' unions, sections of the Bolivian Workers Confederation and the irrigation associations. As part of the broad-based mobilization, more than 10,000 retirees trekked 68 miles to enter La Paz on January 17 to press their demands on the government.</P>
<P>Inflation has severely eroded pensions and benefits for retirees. The protests forced the government to adjust the value of pensions and peg them to the dollar. Significantly, this victory overturned a new law basing the value of pensions on the cost of living rather than on the dollar's value.</P>
<P>Perhaps the most important development to emerge from the recent protests is the creation of an Estado Mayor del Pueblo (EMP, or Peoples' General Staff). The lack of centralization in Bolivia's social struggles has weakened the movement in the past. The EMP will bring greater unity to the movement and allow anti-government actions to pack a greater punch.</P>
<P>In its second official communiqu&eacute; on January 22, the EMP called for intensifying the campaign of roadblocks and mobilizations. It also demanded the resignation of President S&aacute;nchez de Losada for his incompetence and repression.</P>
<P>The EMP has vowed to topple the S&aacute;nchez de Losada's government if he refuses to stop coca eradication and to embark on serious negotiations. This high pitch to the struggle in Bolivia is the latest symbol of the growing resistance to the free market across Latin America. And it shows the potential to challenge Washington's priorities, even in the face of a government willing to use harsh repression.</P>

