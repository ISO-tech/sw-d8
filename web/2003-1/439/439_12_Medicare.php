<P><font face="Times New Roman, Times, serif" size="4">Bush's assault on Medicare and Medicaid</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Blackmailing the elderly</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 7, 2003 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>BLACKMAIL. That's the only way to describe the Bush administration's plan for the federal government's Medicare health program for the elderly.</P>
<P>The White House wants to use the long-promised prescription drug benefit for Medicare recipients to further privatize the system--by offering to pay for drugs only if patients shift to private HMO insurance programs.</P>
<P>But the Bush gang may have even worse in store for Medicaid, the government health program for the poor. Last week, Health and Human Services Secretary Tommy Thompson announced a plan to gut Medicaid by giving state governments the right to "restructure" benefits for one-third of the program's recipients--which is another way of saying that they would be cut off.</P>
<P>Together, this two-pronged assault is a serious threat to millions of the most vulnerable people in the U.S.</P>
<P>The Bush Medicare scam is as sleazy as they come. Legislation for a prescription drug benefit for recipients stalled again last year when Republicans insisted on their version, which would have given subsidies to seniors to buy drug coverage from private insurers. This would have left seniors even more at the mercy of the for-profit health care industry.</P>
<P>But the Bush administration has come up with an even more brazen way to push privatization. The new Bush proposal would bar drug coverage for any senior who decided to stay in the traditional fee-for-service Medicare program.</P>
<P>Only recipients who opted into private insurance programs would get coverage. In other words, outright blackmail to lure the elderly into HMOs--where coverage would be further restricted and decisions about health care left to corporate bureaucrats.</P>
<P>Even many congressional Republicans were skeptical of the Bush plan. For one thing, the HMO giants have been getting out of the Medicare business because it isn't profitable enough. Thus, a pilot program passed under Bill Clinton that tried to shift Medicare recipients into HMOs in return for expanded benefits, including prescription drugs--the misnamed "Medicare+Choice" program--has been a total failure, mainly because private insurers weren't interested.</P>
<P>Bush's $400 billion spending plan for Medicare isn't actually for Medicare. It's yet another government handout to the drug and insurance bosses--all done in the name of "reform."</P>
<P>Not willing to leave bad enough alone, the administration unveiled a new proposal last week that represents an even more sweeping attack on the Medicaid system that covers health care for the poor. Health and Human Services Secretary Tommy Thompson announced that the plan would give states "more power" and "flexibility" to determine how Medicaid patients are covered for health care.</P>
<P>But this is Bush-speak for throwing millions of seniors and the disabled--the majority of people in the Medicaid system--to the wolves. Under Thompson's proposal, state governments would no longer have to get a federal waiver if they wanted to vary from minimum federal standards for eligibility and benefits.</P>
<P>In fact, states that were most aggressive in restricting benefits and increasing charges for services would be rewarded with accelerated funding. "'Flexibility' is a euphemism," health care expert Sara Rosenbaum told the <I>New York Times</I>. "It means you no longer have to insure people with a defined benefit package to which people are entitled. States can buy whatever health services they choose and stop buying services whenever they want. That's not insurance."</P>
<P>Back in the mid-1990s, congressional Republicans under former House Speaker Newt Gingrich proposed a similar scheme for Medicaid--by turning federal funding for the system into "block grants" to state governments to do whatever they pleased with--as part of their Contract on America. The plan caused such an uproar that it went nowhere.</P>
<P>But the Bush administration thinks that it can get away with anything now. It hopes that even if Democrats have enough backbone to oppose the worst outrages, other ones will slip by--and the White House will be further ahead in its all-out assault on governments programs that ordinary people rely on.</P>
<P>We have to expose the Bush gang's fraud--and oppose these attacks on Medicare and Medicaid.</P>


