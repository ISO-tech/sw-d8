<P><font face="Times New Roman, Times, serif" size="5"><b>Say no to Bush's war on Iraq</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 7, 2003 | Page 10</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>NEARLY 8,000 demonstrators marched to the State Capitol in Madison, Wis., on February 1 for a rally dubbed "Pre-emptive Peace Day." Contingents included the Vets for Peace, Moms Against the War and the University of Wisconsin's Stop the War Coalition. The South Central Federation of Labor, which unanimously passed a resolution against an Iraq invasion, turned out a large labor presence.</P>
<P>"It makes me sick to hear Bush say this war is on our behalf," said Joan Grosse, a member of American Federation of Teachers Local 6100. "Well, it's not on my behalf! I'm glad this rally happened. We need more visibility to show that the so-called 'average American' is not with this war."</P>
<P>Speakers stressed organizing for the international day of action on February 15 in New York City. The size of the protest was a welcome surprise, showing that more people are questioning Washington's motives for war in Iraq. </P>
<P>Pittsburgh saw its biggest demonstration since the Vietnam War on January 26, as 5,000 protesters marched through heavy snow to protest the war on Iraq. Afterwards, about 100 people took part in a die-in, lying down in the street to dramatize the deaths of Iraqi civilians. The march was the culmination of a weekend of activities, including teach-ins, workshops, concerts, religious services, a poetry reading, a labor forum and a parade.</P>
<B><P>Sorry State of the Union</P>
</B><P>While Bush broadcast his message of death and destruction during his State of the Union speech on January 28, activists around the country sent a different message: No to war on Iraq! In Washington, D.C., more than 1,500 people gathered outside the Capitol during Bush's address for their own "Sorry State of the Union."</P>
<P>"People come here with all kinds of different concerns, but the one thing that really unites us is the war with Iraq," said John Rockwell, an engineer who traveled from Ohio for the event. While a projection screen showed the live video image of Bush's address, no audio was supplied. So political speakers and bands filled in for Bush's drivel.</P>
<P>"There are a lot of untruths in the media, and the majority of the American people don't want war," Natalie Johnson Lee of the Green Party told the crowd. Jeff Bale, a member of the Washington Teachers' Union and the ISO, spoke about the real aims of Bush's war on Iraq. "The U.S. is the biggest military and economic power in the world, and Bush wants to keep it that way," Bale said. "We need to base our demands around understanding and fighting these imperial aims."</P>
<P>A variety of musical acts, including the Whips, 1905, Thievery Corporation, Mr. Lif and Milemarker, participated. "We're all appalled by Bush's foreign policy, and so the chance to participate in a protest event like this was something we were all really excited about," said Al Burian of Milemarker.</P>
<P>Other events that day included a speakout sponsored by the Berkeley Stop the War Coalition and a 2,000-strong concert and rally in Madison.</P>
<B><P>Campus organizing</P>
</B><P>In the lead-up to the February 15 international day of action in New York City, the New York University Peace Coalition is organizing for a week of action on campus. It will include a teach-in on the war at home, an antiwar film screening and the kickoff of a financial disclosure campaign to divest from corporations that profit from war.</P>
<P>On February 1, almost 300 people gathered at the University of Illinois-Chicago for a teach-in organized by the Chicagoland Student Antiwar Network. Workshops included Media During War, The Cost of War at Home, Artists Against the War, Foreign Policy 101, Dissent and the War Against Civil Liberties.</P>
<P>Speakers included Matt Rothschild, editor of <I>The Progressive;</I> Salim Muwakkil, <I>Chicago Tribune</I> columnist and senior editor of <I>In These Times;</I> and Bill Davis, Vietnam veteran and member of Chicago Labor Against War. </P>
<P>At Berkeley, students are organizing a contingent for the February 16 protest in San Francisco and will meet up with other students in a speak-out at Golden Gate University.</P>
<I><P>Candice Amich, Robin Gee, Gimena Gordillo, Tina Gross, Steve Leigh, Neil Loehlein, Sid Patel and Spring Super contributed to this report.</I></P>


