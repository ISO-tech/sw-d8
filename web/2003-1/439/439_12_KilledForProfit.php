<P><font face="Times New Roman, Times, serif" size="5"><b>Killed for the sake of profit</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 7, 2003 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>VIOLATIONS OF safety standards are the likely cause of the North Carolina factory explosion January 29 that killed four and injured 37. The explosion was felt 10 miles away--and could have killed far more of the 250-strong workforce at the Kinston, N.C., plant, which made syringes.</P>
<P>While the company, West Pharmaceutical Services, had a workplace safety program, representatives of the U.S. Chemical Safety Board investigating the blast have already uncovered dangerous practices in the part of the plant that produced rubber for the syringes.</P>
<P>"Plant employees told the investigators that the rubber mixer on the upper level had experienced previous internal fires, including one strong enough to blow off the mixer door," the board said in a news release. "Investigators do not yet know the number or nature of these fires or whether there is a connection between these mixer fires and the events of January 29."</P>
<P>In fact, West Pharmaceutical has a long record of violating safety and health standards. The company was cited by the federal Occupational Safety and Health Administration (OSHA) 46 times for 71 violations at plants in six states over the past decade.</P>
<P>North Carolina's Labor Department inspected the Kinston plant last November and found 22 "serious" safety violations. It fined the company $9,075. Just a few weeks earlier, federal OSHA inspectors found 30 violations and fined the company $8,690.</P>
<P>But the only other time that the plant was inspected by OSHA was in 1989--14 years after it opened. "With their current budget and staffing--there are 2,238 federal and state OSHA inspectors for 8 million workplaces--it would take OSHA 119 years to inspect every workplace," Lisa Cullen, author of a recent book on workplace safety, told the magazine <I>Multinational Monitor</I> last October. "It is just not possible." The result is that employers like West Pharmaceutical have virtually a free ride to cut corners on workplace safety--with deadly results.</P>
<P>Since 1972, some 200,000 workers have died from on-the-job injuries--and 6,000 more die in workplace accidents every year. When deaths from occupational diseases are included, the death toll rises to 50,000 a year, with millions more injured. The problem is worst in the South, where unions are weak.</P>
<P>A recent <I>New York Times</I> series chronicled the high rate of death on the job at McWane Inc., a leading manufacturer of sewer and water pipes based in Alabama. Since 1995, McWane plants, which employ 5,000 workers, have had 4,600 injuries and 9 deaths.</P>
<P>Some of the most notorious safety violations are in North Carolina, which, despite the growth of industry there in recent decades, has the lowest rate of unionization in the U.S. In the town of Hamlet, N.C., a fire at a chicken plant killed 25 workers in 1991--because managers had locked the exits.</P>
<P>Despite 14 new state laws on workplace safety that followed, employers routinely ignore safety standards. The North Carolina Department of Labor reported that factory deaths declined from 234 in 2001--but were still a staggering 203 last year.</P>
<P>And if George W. Bush has his way, it's going to get worse--in North Carolina and across the U.S. After canceling new ergonomics standards shortly after taking office, Bush now proposes to cut the OSHA budget by 1.7 percent, giving Corporate America even more leeway to risk workers' lives for profits.</P>
<P>The Hamlet fire of 1991 sparked national outrage over dangerous working conditions--and the Kinston explosion should do no less. It should also sound the alarm about the pressing need to rebuild our unions and organize the unorganized--especially in the South.</P>

