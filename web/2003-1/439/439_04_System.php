<P><font face="Times New Roman, Times, serif" size="4"><b>Putin the elf</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>FANS OF the latest Harry Potter movie may notice a striking resemblance between Dobby, the computer-generated house elf, and Russian President Vladimir Putin. A Russian law firm is suing the special effects people who dreamt up Dobby--arguing that the ugly little elf was modeled on Putin himself.</P>
<P>Apparently Potter Web sites and chatrooms have been inundated with angry Russian messages attacking anyone suggesting a likeness between the elf and Putin. Russian radio station Ekho Moskvy has reported on the battles, and Russian TV is preparing to broadcast results of a BBC poll in which more than 7,000 people say Dobby does look like Putin.</P>
<P>It's not the first time the Russian media have lashed out over "misuse" of an image of their president. In 1995, the prosecutor-general investigated alleged insults against Boris Yeltsin in a satirical puppet show, but the court case was later dropped. </P>
<P>--<I>This is London,</I> January 28, 2003</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Pledge allegiance--or get out</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MIDDLE-SCHOOL student Jim Woodbury got a shock when he recently refused to say the Pledge of Allegiance in his U.S. history and constitution class. His teacher, David Laven, made him stand outside the classroom--after hitting the roof when Jim supposedly tried to "incite" other students not to say the pledge.</P>
<P>When Jim presented a packet of information about students' rights to refuse to say the pledge, Laven then allegedly threw the information into a trash can, saying that he didn't care what the law is.</P>
<P>"We are appalled that this can go on in the school," said Jim's mother. Jim, an eighth-grader, said he refused to say the pledge because of his political beliefs. "I believe the flag is a symbol of the government, and I think it's corrupt, and I don't agree with some of the choices it made," he said.</P>
<P>--<I>The Press Democrat, </I>January 8, 2003</P>

