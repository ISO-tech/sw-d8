<P><font face="Times New Roman, Times, serif" size="4">New study exposes "apartheid schools" in U.S.</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Kids who get left behind</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 21, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>Much of the debate around Bush's "No Child Left Behind" education bill has left out the reality of public schools. Grandiose claims of what schools "should" be able to do have been plenty, while reality has been ignored. That's what made the recent report on segregation from Harvard's Civil Rights Project so refreshing--and so desperately needed.</P>
<P>After analyzing school enrollment data across the country for 2001, the researchers discovered that public schools in the U.S. are rapidly resegregating--not getting more diverse. "When America celebrates Martin Luther King Jr. Day, students in schools named after Dr. King will be reciting the 'I Have a Dream' speech in auditoriums where there are no white students, and almost everyone is poor enough to get a free lunch, the very kind of school Dr. King fought to eliminate," says the report.</P>
<P>The report concluded that Black students, who had attended increasingly integrated schools from the 1950s through the 1980s, were now trapped in schools segregated at levels not seen in three decades. In fact, researchers found an astonishing number of schools with virtually no white students, which they aptly labeled "apartheid schools." </P>
<P>These schools housed one-sixth of all Black students in the U.S., and one quarter of Black and Latino students in the Northeast and Midwest. Latino students, according to the study, are actually the most segregated minority group in America's schools--divided by race, poverty and language. </P>
<P>What makes the trend even worse is that minority students now account for 40 percent of the total student population, almost twice the level of the 1960s. This means that a much more substantial number of our youth are stuck in segregated, deteriorating schools.</P>
<P>It is that reality which Bush and his cronies say can be fixed with a few more standardized tests, a few more lofty "standards" and by letting private companies take over. We know the only thing that will fix our schools is money--like the $400 billion the Pentagon will get this year. </P>
<B><P>Jeff Bale, </B>Washington, D.C.</P>
