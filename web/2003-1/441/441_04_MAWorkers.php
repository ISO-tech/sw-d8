<P><font face="Times New Roman, Times, serif" size="5"><b>Standing up to the war at home and abroad</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 21, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>I'm writing to share a local victory against Bush's war drive. On January 22 in Lynn, Mass., the North Shore Labor Council passed an excellent resolution against the war.</P>
<P>The resolution reads in part: "Whereas, the Administration's real aim in this war is to control Iraq's oil, increasing corporate profits at the expense of millions of working people and at the possible cost of tens of thousands of lives and vast destruction...</P>
<P>"Whereas the billions spent on armaments, domestic repression and bailouts could be better used to provide retraining programs and jobs...and to plug the $50 billion deficit in state and local budgets that has resulted in major loss of jobs and cuts in essential social services&#133;"</P>
<P>The driving force behind the North Shore Labor Council is IUE-CWA Local 201 at General Electric, whose 3,000 members were recently on strike for two days as part of the national strike against GE's attack on health care benefits.</P>
<P>At the council meeting, several GE workers spoke about the significant amount of antiwar sentiment among their coworkers. One longtime GE worker described how surprised he has been that his young coworkers are overwhelmingly against war. </P>
<P>He told the meeting that these are the workers who build aircraft engines for the military, workers who are the most vulnerable to layoffs because of low seniority, and the group who should be the most susceptible to arguments that war would be good for them economically. But they aren't buying it.</P>
<P>This just shows the depth of antiwar sentiment among workers and the potential for building an antiwar movement where labor plays a leading role.</P>
<B><P>Annie Levin,</B> Boston</P>

