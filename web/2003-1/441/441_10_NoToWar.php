<P><font face="Times New Roman, Times, serif" size="5"><b>Say no to Bush's war on Iraq</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 21, 2003 | Page 10</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>STUDENT ANTIWAR activists from around the country gathered February 15, after the 500,000-strong antiwar protests in New York City to say no to George W. Bush's war on Iraq. About 1,000 people turned out for the forum, sponsored by the Colombia University Antiwar Coalition.</P>
<P>Dalia Hashad from the American Civil Liberties Union Immigrants Rights Project led the room in a loud shout of "No!" "Remember that 'No!' when you go back to your school, when there is a time when you feel like you are standing alone," she told the crowd. "Because this is going to be a long fight. Remember the sound in this room and don't be afraid to fight."</P>
<P>Hamid Dabashi from Columbia University read a letter he'd composed especially for Secretary of State Colin Powell after he addressed the United Nations (UN) Security Council. "We refuse to be silenced and blinded," Dabashi declared, shaking with anger. "Tonight in New York, we join millions of our brothers and sisters in Iraq and the rest of the Arab world, in Asia and in Africa, in Europe and Latin America, and we declare, sir, that we are the innocent civilian victims of a band of terrorists&#133;who have kidnapped our civil liberties and endangered our security."</P>
<P>"Is it only Americans that are created equal or is it the entire world?" asked Scott Ritter, a former UN weapons inspector, who is now an outspoken critic of Bush's war plans in Iraq. "No war with Iraq. Not now, not ever. Not in my name, not in your name, not in our name!" Ritter declared.</P>
<P>"If George Bush and the people who run New York can't assure democracy on the streets of New York, damn hell if they're going to assure democracy to the Middle East," said Ahmed Shawki, editor of the <I>International Socialist Review,</I> referring to the NYPD's strong-arm tactics on protesters all day long.</P>
<P>"They are talking about a war, an occupation in Iraq and future wars," Shawki explained. "So, if there is any message for today, we fight the fight to prevent this war. If the bombs drop, we have to redouble our efforts against that war and make it a war they can't wage without paying a very, very high price at home&#133;Our task is regime change at home."</P>
<P>This incredible event also included speeches by Michael Letwin, co-convenor of New York Labor Against the War; Mike Marqusee from the Stop the War Coalition in Britain; Amy Goodman of <I>Democracy Now!</I> radio; Anthony Arnove, editor of <I>Iraq Under Seige</I> and a member of the ISO; and music from the Welfare Poets, among many others.</P>
<B><P>Even in Bush country</P>
</B><P>On February 12, thousands of antiwar students staged the first-ever walkout at the University of Texas (UT) in Austin to tell George W. Bush that he's not getting their support for his war. Some 5,000 students, professors, staff and community members marched through campus and rallied at the Tower at the center of campus.</P>
<P>"This is totally unexpected to see so many people out here against the war," said protester Steven Gross. A coalition of groups, including the ISO, the Campus Coalition for Peace and Justice and UT Watch, spent long hours building for the march. A host of student and activist speakers, including Rahul Mahajan, activist and author of <I>The New Crusade: America's War on Terrorism,</I> and Dana Cloud, UT professor and ISO member, rallied the crowd. Austin activists are sending a message from "Bush country": No to racism! No to war in Iraq!</P>
<B><P>More campus organizing </P>
</B><P>In Burlington, Vt., as part of the week of action leading up to the New York protest on February 15, University of Vermont Students Against War held a campus rally against war on Iraq. Despite bitter temperatures, about 200 people turned out.</P>
<P>Passersby stopped to listen to the speakers and find out more about why they should oppose war on Iraq. "As there has been an escalation of the conflict, awareness on campuses has risen," a student named Emily said. "This campus is opening its eyes."</P>
<I><P>Brian Grant and Matt Korn contributed to this report.</P>
