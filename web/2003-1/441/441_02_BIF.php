<P><font face="Times New Roman, Times, serif" size="4">Feds force plea from Muslim charity head</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>No justice in this witch-hunt</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 21, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE FEDS couldn't contain their smugness last week as Enaam Arnaout, leader of the Palos Hills, Ill., charity Benevolence International Foundation (BIF), pled guilty to a single count of racketeering. "To take a bird in the hand and short-circuit a trial and get him talking was very satisfying," crowed federal prosecutor Patrick Fitzgerald.</P>
<P>Prosecutors agreed to drop other charges and seek a 20-year sentence, in return for Arnaout "admitting" that, beginning in 1993, he and others at the charity used donations to support Islamist rebels in Chechnya and Bosnia-Herzegovina with boots, tents, uniforms and, in one case, an ambulance. </P>
<P>The Justice Department claims that this was support for "terrorism." Yet the U.S. government was supporting some of these very same forces at the same time. U.S. Ambassador to Croatia Peter Galbraith has even admitted to Congress that he approved a deal in 1994 to transfer weapons from Iran through Croatia into Bosnia. "I believed then, and even more strongly now, that the administration made the right decision," Galbraith told Congress two years later. </P>
<P>So when will Galbraith be indicted? The answer is never.</P>
<P>Arnaout has been sitting in a maximum-security jail cell for nearly a year because he dared to challenge the Bush administration's witch-hunt against Arabs and Muslims. He was originally charged with perjury for denying any BIF connection to terrorist or military organizations.</P>
<P>But when that case was thrown out of court, the Justice Department then alleged that Arnaout was directly linked to the al-Qaeda network. Their proof? Notes supposedly "discovered" at the BIF's offices that summarize the 1988 meetings in Afghanistan at which al-Qaeda was formed.</P>
<P>Arnaout's plea deal--which does not admit any connection to al-Qaeda--shows the scale of the pressure he faced from the U.S. officials and "the never-ending media frenzy about al-Qaeda and Osama bin Laden," as his lawyer, Joseph Duffy, put it. "One has to question whether a fair and impartial jury could be found anywhere in America today that could sit in judgment of an Arab-American in a case involving allegations of terrorism," Duffy said.</P>
<P>John Ashcroft and his goons never stopped smirking for the TV cameras. "We will ensure that terrorist sympathizers who fund violence or terrorism meet swift, certain justice," Ashcroft declared. But there's nothing just about this witch-hunt.</P>


