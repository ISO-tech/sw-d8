<P><font face="Times New Roman, Times, serif" size="4">Student activists head to Chicago for conference</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"The only way to build is from the bottom up"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 21, 2003 | Page 2</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>STUDENT ACTIVISTS from around the country will gather in Chicago this weekend for a national conference of the Campus Antiwar Network (CAN). CAN was formed at two meetings in Washington, D.C., and San Francisco on January 17, where delegates representing more than 70 high schools and universities met to discuss their activities--and then hooked up via speakerphone to make decisions. The Chicago conference will feature discussions of CAN's points of unity and organizational structure, as well as educational workshops--with two delegates from each campus antiwar group.</P>
<I><P>Socialist Worker</I> asked student activists around the country about the significance of the formation of a national student antiwar network.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">

<B><P>JESSICA JONES<BR>
</B><I>George Washington University Students Against the War in Iraq</P>
</I><P>We're going to meet in Chicago, and in a very democratic way, with every school represented by two votes, we're going to set up our own structure and our own grassroots way of organizing. Because the only way you really build an antiwar effort, especially on a college campus, is from the bottom up.</P>
<P>With each conference we've had, we expand. And everyone who went to the conferences last time that I talked to came out with a really positive feeling--that this is the way to do things. </P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">

<B><P>JESSICA MAIORCA<BR>
</B><I>University of Illinois-Chicago No War</P>
</I><P>I think this will get a lot of students ready for future actions. I think a lot of students enter movements with a strong feeling about something, and they come out realizing that there's a larger pattern in the United States' actions. </P>
<P>I'm expecting a lot of debate over what should be the immediate steps that CAN should take. Of course, we have different political ideologies being represented that can definitely clash. But it comes down to patience and compassion and realizing why we're together. </P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">

<B><P>WARREN CRAIG<BR>
</B><I>University of Texas Campus Coalition for Peace and Justice</P>
</I><P>The goal is to get as many campuses and as many students together as possible to democratically plan out a future for students within the antiwar movement. We want to strengthen all of our bases on the campuses.</P>
<P>And there are educational meetings planned--probably eight to 10 different workshops, on topics like "Iraq from the Gulf War to the present," "Fighting the crackdown on civil liberties," "Bush's war aims in the Middle East" and "After Iraq: What's the next step in the war on terror."</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">

<B><P>MONIQUE DOLS<BR>
</B><I>Columbia University Antiwar Coalition</P>
</I><P>We're the only group nationally where you see students involved not only in building bases on campuses, but who are shaping the direction of the national organization.</P>
<P>We mobilize for large demonstrations because that gives people a sense of how strong our movement is and how big we are nationally and internationally. But we have to be able to come back and build the bases locally where we live--and then come together with a sense of how we can shape our own movement.</P>
<P>You can't replace that democracy and that sense of participation. I don't think there's a shortcut. This is a necessary building block for a movement where people have a voice.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">

<B><P>TONY SHAFER<BR>
</B><I>San Diego State University Counterattack</P>
</I><P>The greatest concern is trying to come up with a way to battle basically a Goliath-type force that is the administration, and their overwhelming desire to go to war with Iraq. This is a network and alliance of students across America, which is somewhat representative of the youth of America. This gives them greater access to each other and to voice their opinion against the war. </P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">

<B><P>MICHAEL SMITH<BR>
</B><I>University of California-Berkeley Stop the War Coalition</P>
</I><P>What we can accomplish in Chicago is to meet each other and find out what we're doing on our different campuses, and we can come away with new ideas and new energy. Also, we can come out with concrete proposals for things that we can do nationally. </P>
<P>Right now, we're working on the model where one group will call an action, and everybody across the country will take part in it or not. And the problem with that is there's not a lot of decision-making at the grassroots level, where students at each campus can plug in and decide the direction that the antiwar movement is going to take. </P>
<P>So if we can come away from this with some concrete proposals that will be decided democratically by all the different campuses from around the country, I think that would be tremendous.</P>


