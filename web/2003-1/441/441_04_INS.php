<P><font face="Times New Roman, Times, serif" size="5"><b>Racially profiled in San Diego</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 21, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>In what has been termed "Operation Game Day," the Immigration and Naturalization Service (INS) arrested dozens of foreign-born security guards and transportation workers in San Diego who might have had access to special events and secure areas related to this year's Super Bowl. </P>
<P>The INS targeted workers, most from Latin America, on the basis that they came from countries suspected of having terrorist cells. While some of those arrested did have criminal records, this does not excuse the fact that they were originally detained solely because of their ethnicity. The INS has yet to prove that any of the workers arrested are actually linked to terrorism.</P>
<P>The message sent by the INS is clear: Our government will continue to exploit the events of September 11 to further racial profiling and working-class division. We have to organize our communities against any more of these racist detentions. </P>
<B><P>Marjorie Berry,</B> San Diego</P>

