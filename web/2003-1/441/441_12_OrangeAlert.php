<font face="Times New Roman, Times, serif" size="5"><b>Their "orange alert" fraud</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 21, 2003 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IT WAS like a scene out of the 1950s Cold War. Only instead of duck-and-cover drills against a feared nuclear attack, the Bush administration put the country on an "orange alert" against a supposedly "likely" terrorist attack.</P>
<P>In New York City, 16,000 extra cops, equipped with radiation detectors and gas masks, were added to patrol the subways, train stations and airports. In Washington, D.C, anti-aircraft missile launchers were parked around the city--and members of Congress were told to gather up supplies, sensitive documents, medicine and a laminated list of key phone numbers in case they had to leave quickly. </P>
<P>And in the most bizarre episode of all, Homeland Security chief Tom Ridge announced a "Ready Campaign," encouraging families to designate a room to take refuge in during a chemical attack--and to buy plastic sheeting and duct tape to seal it off. "Terrorists give us a choice," Ridge said on CNN's <I>Late Edition</I>. "We can either be afraid, or we can be ready."</P>
<P>But scaring people is precisely what this "orange alert" is all about--and it's the Bush gang that's doing the scaring.</P>
<P>From the beginning, experts said that duct tape and plastic sheeting would do nothing at all to protect people from a chemical or biological attack. And ABC News finally reported on February 13 that the claim made by a detainee at the military's Guant&aacute;namo Bay gulag for suspected "terrorists" that Washington, New York or Florida would be hit by a "dirty bomb" last weekend "had proven to be a product of his imagination."</P>
<P>But that didn't stop the corporate media from jumping on board the Bush administration's fear wagon. "We may very well get hit, let's hope we survive it," pundit Morton Kondracke declared on the Fox News Channel. </P>
<P>But not everybody was buying the Bush administration's scare tactics. "There's no plausible explanation other than an attempt to ratchet up the hysteria level in hopes it draws attention," Norman Solomon, executive director of the Institute for Public Accuracy, told <I>Salon</I> magazine. </P>
<P>Among the 1 million people who demonstrated against the war at protests across the country last weekend, many were suspicious that the "orange alert" was timed to scare activists into silence. One sign carried by a protester in New York was headlined with the words "The proper use of duct tape"--and showed a picture of Colin Powell with his mouth taped shut.</P>
<P>The hysterical warnings about chemical attacks on U.S. cities mask an even bigger hypocrisy as well. According to Britain's <I>Independent </I>newspaper, Defense Secretary Donald Rumsfeld told a congressional committee this month that U.S. forces are planning to use "non-lethal" biochemical weapons, such as anti-riot gases and crowd control agents, if they invade Iraq. These are the same chemicals that the Russian government used last year to end a hostage standoff in a Moscow theater--and killed more than 100 people.</P>
<P>The Bush gang claims that its "orange alert" will "prepare" Americans for the likelihood of a terrorist attack. But they're the ones who are a preparing a terrorist attack--and millions of Iraqis will pay the price.</P>


