<P><font face="Times New Roman, Times, serif" size="4"><b>Cover-up at the UN</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SOME MASTERPIECES are too hot to handle. Workers at United Nations (UN) headquarters in New York City recently draped Picasso's famous painting "Guernica" with a large curtain.</P>
<P>The painting, a replica that has hung outside the UN since 1985, depicts the terrorized and dying civilians at Guernica, a small Basque village in northern Spain, which Gen. Francisco Franco's fascists allowed the German air force to use for target practice during the Spanish Civil War. </P>
<P>Television cameras routinely scan the tapestry as diplomats enter and leave the council chambers. But with the U.S. taking its case for war on Iraq to the UN, some feel that the painting is in "poor taste."</P>
<P>UN officials said that it's more appropriate for "dignitaries" to be photographed in front of a blue backdrop and some flags, rather than the impressionist image of shattered villagers and livestock. "It's only temporary," said Abdellatif Kabbaj, the organization's media liaison. "We're only doing this until the cameras leave."</P>
<P>So the drapes were put up on the days that the council discussed Iraq--and came down on the days when the subjects included Afghanistan and peacekeeping missions in Lebanon and Western Sahara.</P>
<P>--<I>Washington Times,</I> February 3, 2003</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>"Choosing" to be an idiot</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ONE CONSERVATIVE South Carolina state representative says that he has a compromise in the recent debate over proposed license plates with the word "Choose Life." License plate tags that say "Choose Death."</P>
<P>The law allowing the "Choose Life" plate was passed in 2001, but Planned Parenthood and other groups sued to keep the anti-choice tags off the road. Now, anti-choice state Rep. John Graham Altman says that pro-choice people can buy a "Chose Death" plate instead--seeing as how he thinks that they're baby killers.</P>
<P>"My bill is simply a reaction to the abortionists," said Altman. "They're pro-choice. Well, they've got a choice--whether to buy [the license plate tag] or not." </P>
<P>--Associated Press, January 24, 2003</P>

