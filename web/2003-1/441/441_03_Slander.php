<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>A slander on our movement</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 21, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>AS ANTIWAR activists across the country were building for the largest possible turnout at last weekend's demonstrations, liberal critics of the movement grabbed headlines with a red-baiting attack on organizers of the San Francisco protest.</P>
<P>In a guest editorial titled "Anti-War Anti-Semites" in the <I>Wall Street Journal, </I>Rabbi Michael Lerner, leader of the liberal Jewish organization Tikkun, claimed that he had been "banned" from speaking in San Francisco--because organizers were anti-Semites who refused to let him express his pro-Israel point of view. Specifically, Lerner charged that he had been "blackballed" by the antiwar group International ANSWER. His evidence for ANSWER's "anti-Semitism"? The fact that the group strongly condemns Israel's oppression of the Palestinians.</P>
<P>In short order, liberal writers who have been attacking ANSWER for months--like David Corn in the <I>Nation </I>and Michelle Goldberg at <I>Salon</I>--had waded in with more abuse, even organizing an online petition through the <I>Nation </I>to "defend" Lerner without ever bothering to learn the facts.</P>
<P>Lerner himself spent the run-up to the February 16 demonstration smearing march organizers in high-profile TV interviews and in the corporate press--all the while claiming to be "antiwar." </P>
<P>In fact, Lerner's charges are entirely groundless. His group Tikkun is a member of United for Peace and Justice, one of four coalitions (the other three being Bay Area United for Peace, Not In Our Name and ANSWER) that jointly organized the march.</P>
<P>In the interest of working together rather than fighting each other, the groups agreed not to put forward speakers who had attacked the others. In spite of this agreement, members of United for Peace and Justice asked Tikkun if Lerner wanted to speak--and said that though he has frequently red-baited ANSWER in the mainstream press, they would nevertheless put his name forward.</P>
<P>A Tikkun representative at that meeting repeatedly said that there was no need for Lerner to speak. Had his name been proposed at that time, in all likelihood, he would have been asked, even though this would have violated the coalitions' agreement.</P>
<P>Instead, when Lerner discovered that his staff hadn't put his name forward, rather than go to the organizers, he issued a press release claiming that ANSWER and the other coalitions had "banned" him--and that this was because of his pro-Israel views, an issue that had never arisen in any discussion. The march organizers first read about the "ban" they had supposedly imposed in the <I>San Francisco Chronicle.</I> </P>
<P>With a few days to go before the demonstration, there was a discussion within the coalitions about whether to grant Lerner a place to speak in order to disprove his accusations. But most organizers rightly felt that Lerner's week of vilifying the antiwar movement meant that he had crossed a line--and that it would be wrong to cut the time of a speaker who is actually helping build the antiwar movement.</P>
<P>Lerner's frantic attacks--in the pages of the <I>Wall Street Journal, </I>no less--will have discredited him in many activists' eyes. But the charge of anti-Semitism will have confused others. It is a baseless slander--and one that has been used before, by liberals and right wingers alike, to attack those who stand for justice, by equating their opposition to Israel's war on Palestinians with bigotry toward Jews.</P>
<P>As a speaker from A Jewish Voice for Peace said at the San Francisco rally, "We condemn the oppression of the Palestinians. And we condemn anti-Semitism just as strongly. But we also condemn those who would use false charges of anti-Semitism to cover up the suffering of the Palestinians and to divide the antiwar movement. It isn't anti-Semitic to be pro-Palestine, and it isn't anti-Semitic to be antiwar."</P>
<P>Lerner and his supporters have an agenda. By smearing other activists and organizations, they hope to advance their own politics within the antiwar movement. Red-baiting and slanders about anti-Semitism have no place in our struggle, and we shouldn't stand for them.</P>

