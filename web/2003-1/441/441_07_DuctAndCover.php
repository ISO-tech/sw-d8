<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Ridge's "duct and cover" madness</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 21, 2003 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ON MONDAY, Homeland Security Secretary Tom Ridge quietly announced plans to cancel the "code orange" terrorist alert declared by the Bush administration on February 7. Ridge's admission that intelligence sources were "not as accurate as we thought" was self-evident, since the attack that was supposed to take place never materialized.</P>
<P>Just days earlier, Ridge berated cynics with a stern warning that the orange alert was based on "multiple sources, obviously credible and corroborated," and that an attack was imminent, with Washington, D.C., and New York the likeliest targets.</P>
<P>The orange alert allowed the Bush administration to set in motion a series of coordinated attempts to ratchet up the panic level across the country, accompanied by a media frenzy treating television viewers to round-the-clock scenarios of possible terrorist attacks.</P>
<P>No sooner did Bush raise the terror threat alert than Secretary of State Colin Powell unveiled a tape allegedly featuring Osama bin Laden's voice urging his followers "in code" to attack U.S. targets in retaliation for a war on Iraq. Powell claimed the tape provided the missing evidence that al-Qaeda is linked with the Iraqi regime, bound by a "common hatred" of the U.S. CIA director George Tenet was hauled before Congress to offer credibility to this preposterous claim, even though bin Laden and Saddam Hussein are well-known adversaries.</P>
<P>Meanwhile, the Department of Homeland Security urged residents to order the manual, "Are you Ready? A Guide to Citizen Preparedness." The manual instructs citizens to protect their homes from a chemical or biological attack by stocking up on duct tape and plastic to seal up rooms, and water, in case the water supply becomes contaminated.</P>
<P>Most experts agree this advice is roughly as effective as the "duck-and-cover" drills in U.S. schools during the Cold War, when children were told to hide under their desks during a Soviet nuclear bomb attack. The recent hype bore other similarities to the anticommunist paranoia of the Cold War era--when fluoridation of the water supply was deemed a "communist plot" in many circles.</P>
<P>The Health Department circulated a memo last week telling New York-area hospitals, "antidotes are available for cyanide and nerve agents"--raising the specter of an impending chemical attack. The NYPD increased the police presence throughout New York City subways, as large groups of armed cops swept through in search of "suspicious-looking people."</P>
<P>U.S. residents were told to beware of men with nicks on their shaved faces--they may be al-Qaeda operatives in disguise. Seemingly harmless soda bottles lurking in subways could be ready to unleash a chemical weapons attack. Watch out for the smell of Juicy Fruit gum--cyanide smells just like it.</P>
<P>Last week, FBI agents visited chemical plants and demanded customer lists, while Ridge urged corporate managers to check for "infiltrators" among their employees. The alert also gave New York authorities the excuse they desired to deny a march permit for the antiwar protest scheduled for February 15.</P>
<P>Using the same excuse, they banned portable toilets (those well-known terrorist hideouts) at the rally site, stationed police sharpshooters on roof tops and squads of cops in riot gear around Manhattan to physically prevent thousands of protesters from reaching the rally site.</P>
<P>But even as the terror alert reached a fever pitch on Saturday--coinciding with the antiwar protest--its impact was distinctly muted. In a February 12 Fox News poll, 74 percent of respondents said they had not made the suggested emergency preparations for a terrorist attack. And the economy--not terrorism--continues to rank as the public's number one concern.</P>
<P>Most importantly--although many thousands were forced to storm police barricades to reach the protest--half a million people demonstrated against the war in New York on February 15. The Bush administration's plan backfired--its transparent attempt to use a vague terrorist threat to undercut opposition to the war only strengthened it.</P>
<P>Anything al-Qaeda or Saddam Hussein might have in mind pales in comparison to the reign of terror the U.S. plans to unleash on the people of Iraq.</P>


