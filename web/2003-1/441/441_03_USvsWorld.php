<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">The Bush gang wants its war--and protesters be damned</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Washington vs. the world</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 21, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WITH 10 million people marching around the world against the U.S. war on Iraq, Washington finally got the message last weekend about just how isolated it has become.</P>
<P>"The fracturing of the Western alliance over Iraq and the huge antiwar demonstrations around the world this weekend are reminders that there may still be two superpowers on the planet: the United States and world public opinion," the <I>New York Times</I> observed.</P>
<P>So what has been the response of the Bush administration and its allies? We'll have our war anyway--the 10 million who protested be damned. "We need to remind everybody that tyrants don't respond to any kind of appeasement," National Security Adviser Condoleezza Rice sneered on a TV talk show. "Tyrants respond to toughness."</P>
<P>Sen. John McCain (R-Ariz.) declared that protesters were "unwise" to demonstrate--and added that France's opposition to a second United Nations (UN) Security Council resolution authorizing war reminded him of an aging actress trying to rely on her looks when she "doesn't have the face for it."</P>
<P>Resistance to Washington by France, Germany and Belgium was hailed in many handmade signs at the enormous February 15 demonstrations. But the limit of those governments' opposition to Washington became clear just a day after the mass demonstrations, when they caved to U.S. demands that NATO provide military support to member country Turkey to "defend" itself against Iraq. As if the coming war wasn't an unprovoked attack on Iraq!</P>
<P>The <I>Wall Street Journal</I> spelled out the real dynamics of the split between Washington and its longtime Western European allies. "The U.S., as set out in its National Security Strategy last year, aims to use its status as the lone superpower to reorder the world," the <I>Journal</I> wrote. "France wants to prevent the American Goliath from ruling the world unilaterally."</P>
<P>This is what's behind the Bush administration's talk about a pro-U.S. "new Europe"--basically a combination of conservative governments in Spain, Italy and the Netherlands, and the small Eastern European states that used to be part of the USSR's empire, but now take their orders from Washington instead.</P>
<P>No one should believe that France won't still sign up for the U.S.-led war on Iraq--on the grounds that if you can't beat Washington, you better join them. Odds are that Bush will get his war--and he's already preparing a countdown to invasion.</P>
<P>But even if NATO is patched back together and the UN authorizes war on Iraq, the splits between Washington and its longtime allies are a fact--and a sign of further conflicts to come. "There is still too little appreciation of the scale of the coming geopolitical earthquake," wrote <I>Financial Time</I>s columnist Philip Stephens. "American occupation of Iraq--and let us not delude ourselves, this will be a long-term commitment--will do more than redraw the region's strategic map. It will mark the moment when the U.S. takes upon itself a role that it has disavowed since the annexation of the Philippines more than a century ago--the role of the imperial power.</P>
<P>"For the past 50 years, America has ruled a virtual empire, its power rooted as much in its economic strength and in admiration for its liberal values as in the aircraft carrier fleets that project its military prowess. Deposing Mr. Hussein will make America's imperium as real as those of Britain and France a century ago." </P>
<P>Yet the clearer that Washington's war aims have become, the larger the global opposition. The driving force of that opposition isn't in the presidential suites or United Nations meeting rooms, but among the international mass movement that showed itself so spectacularly February 15.</P>
<P>"For the moment, an exceptional phenomenon has appeared on the streets of world cities," the <I>New York Times</I> had to admit. "It may not be as profound as the people's revolutions across Eastern Europe in 1989 or in Europe's class struggles of 1848, but politicians and leaders are unlikely to ignore it."</P>
<P>That is where the power to stop the U.S. war machine lies. But succeeding in this will require the antiwar movement to oppose not only the U.S. attack on Iraq, but Washington's whole drive to world domination.</P>


