<P><font face="Times New Roman, Times, serif" size="4">GOP's welfare plan</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Taking aim at the most vulnerable</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 21, 2003 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE REPUBLICAN steamroller is running at full throttle--and it's aimed at America's poor. Last week, House Republicans voted overwhelmingly in favor of legislation that will heap further restrictions on welfare recipients.</P>
<P>The bill, which passed 230-192 and now goes to the Senate for debate, seeks to extend the number of hours that welfare recipients must work or be involved in other scheduled activities, such as job training, to 40 hours. That's 10 more hours added to the already punitive work requirement in the welfare "reform" legislation that was passed in 1996.</P>
<P>In the five years since the Clinton administration's "Personal Responsibility Act" became law, the welfare rolls have been cut by half--to about 8 million people. Rep. Rahm Emanuel (D-Ill.), who, as a Clinton advisor, helped write the 1996 bill, said the law had succeeded by "linking a generation of children to the culture of work."</P>
<P>However, many who left the rolls now have to endure desperate poverty and dead-end, low-wage jobs. Plus, as decent jobs disappear during the current economic crisis, millions more workers will be added to this list.</P>
<P>While the Department of Health and Human Services claims that welfare cases continue to decline, the Washington-based Center for Law and Social Policy reported last month that caseloads rose in three-quarters of U.S. states from July to September 2002.</P>
<P>The House Republicans want to make matters worse. Their legislation would require states to increase the percentage of adult welfare recipients who must hold jobs from 50 percent to 70 percent by 2008.</P>
<P>The hardest hit will be single mothers. While Republicans want to require mothers to work more hours, they are only adding $2 billion over five years to the $4.8 billion annual grant for child care--not even close to enough to meet the need. And GOP leaders say that half of that $2 billion could be cut out--if lawmakers decided that there wasn't room for it in the federal budget.</P>
<P>But these politicians think that there's money to burn for programs to appease the religious right--like $300 million to help foster "healthy marriages." The legislation also includes a $50 million yearly subsidy to programs that encourage people to abstain from sex before marriage and bans any discussion of contraception. </P>
<P>And it bars legal immigrants from ever collecting benefits. A similar provision was passed in 1996, but it caused such outrage that Congress had to backtrack.</P>
<P>George W. Bush has already hailed the welfare legislation, calling it a "compassionate approach will help many more Americans realize a better life of independence, hope and dignity that comes with having a job." </P>
<P>What garbage! We have to speak out against this attack on the poorest in U.S. society--and organize against the Republicans' right-wing attack.</P>

