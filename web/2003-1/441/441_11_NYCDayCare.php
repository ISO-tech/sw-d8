<P><font face="Times New Roman, Times, serif" size="4">NYC union takes on mayor's push for concessions</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Day care workers fight back</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 21, 2003 | Page 11</font> </P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Some 7,000 day care workers held a one-day strike February 12 to demand raises in line with those received by municipal workers here.</P>
<P>New York City Mayor Michael Bloomberg denounced the strikers for disrupting care for 50,000 children. The workers--members of AFSCME District Council (DC) 1707--gave their reply at a spirited City Hall rally. "This is not against children," their picket signs read. "This is against Bloomberg."</P>
<P>While the day care centers are privately run, they are largely funded by the city, so Bloomberg is effectively the workers' boss. At the rally--attended by a number of labor leaders and politicians--DC 1707 leaders pointed out that the city has received $136 million in federal funds earmarked for day care over the last two years, yet it cut the amount it spent on such programs by $92 million.</P>
<P>The city's handling of these funds is at the center of a lawsuit brought by the union against the city. "Millions of dollars have come into the city from the federal government for child care," Michael Green of DC 1707 told reporters. "And they have just diverted the money, illegally, to fill their budget deficits. The children and the workers in this city shouldn't be paying for that." </P>
<P>Day care workers have been working without a contract for two years and demand a settlement patterned after that negotiated by other city unions--a 9 percent raise spread over a 27-month contract. But city officials are offering day care workers a five-year contract with no increase in base salaries, instead proposing lump-sum 3 percent payments each year.</P>
<P>And even those payments won't be made unless the union agrees to "productivity increases"--that is, cutting vacation time from four to six weeks to a maximum of two weeks and $15 co-payments for doctor visits. This is an outrage.</P>
<P>Cooks and custodians at day care centers are paid just $21,000 per year. Day care teachers earn between $29,000 and $39,000 per year. Even those certified by the Board of Education start at just $34,000 per year, compared to $39,000 for board-certified teachers in the New York City public schools. "People who do pet grooming make a lot more per hour than people who do child care," City Council member Bill de Blasio said at the City Hall rally.</P>
<P>Union leaders have vowed to step up the fight. DC 1707 President Brenda Stokely said that the day care workers have the support of a broad coalition of labor and community groups who see this fight as crucial to the struggle against Bloomberg's efforts to solve the city's budget crisis on the backs of working people.</P>
<P>Labor contracts covering about 300,000 municipal workers are soon to be negotiated--and the outcome of this struggle will set a benchmark for those talks. That's why it's crucial to build solidarity in the New York day care workers' fight for justice.</P>


