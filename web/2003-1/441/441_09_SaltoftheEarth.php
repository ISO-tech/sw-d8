<P><font face="Times New Roman, Times, serif" size="4">The 50th anniversary of <I>Salt of the Earth</I></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>A film born of struggle</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 21, 2003 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>A MOTION picture "born from the concept of struggle, made in struggle, and to be released only through struggle." That's how blacklisted Hollywood screenwriter Albert Maltz described the movie <I>Salt of the Earth,</I> which turns 50 years old this year.</P>
<P>Filmed in 1953 in the midst of the McCarthyite witch-hunts, <I>Salt of the Earth</I> tells the story of a real strike of mostly Mexican American mineworkers that took place in Grant County, New Mexico.</P>
<P>The workers' 15-month strike against Empire Zinc began in 1950--the same year that the Congress of Industrial Organizations (CIO) union federation carried out a great purge of leftists in its affiliated unions.</P>
<P>The CIO fired every union official who had ties to the Communist Party and ousted some 11 left-led unions from the federation. The International Union of Mine, Mill and Smelter Workers was one of those unions.</P>
<P>The Mine-Mill union had built its reputation as a fighting union, with a priority on organizing workers traditionally ignored by other unions, particularly Black and Mexican American workers. It also fought for unity among minority and white workers, who were regularly pitted against one another by the bosses.</P>
<P>The bosses at Empire Zinc used the Red Scare to try to discredit the strike, claiming that it was undermining the war effort in Korea. They also tried to portray Mexican American workers as the mere dupes of leftists in Mine-Mill Local 890.</P>
<P>Nothing could be further from the truth. Local 890 organizer Clinton Jencks saw the role of workers leading themselves as central. And with each obstacle that the employers put in their way, strikers developed a new strategy to combat them.</P>
<P>When Grant County officials granted the company an injunction against banning strikers from holding pickets, workers were forced to improvise. Although there was initial opposition from the men, women took up the picket duty. And when the police rushed them, the women fought back and went to jail.</P>
<P>Scenes in <I>Salt of the Earth</I> that capture these moments--with strikers' wives and children driving the sheriff's officers nuts as they chant at the top of their lungs from the crowded cells in the county jail--make this an exhilarating film to watch. But more than simply depicting that struggle, <I>Salt of the Earth</I> was part of that struggle--and was itself a struggle of sorts.</P>
<P>When a team of blacklisted filmmakers--producer Paul Jarrico, director Herbert Biberman and screenwriter Michael Wilson--decided in 1951 to make a movie about the strikers' story, they knew exactly what they were getting into. They were among the thousands of Communist Party members, sympathizers and other leftists who had been drummed out of Hollywood during the McCarthyite witch-hunts. For Jarrico, making <I>Salt of the Earth</I> amounted to committing "a crime to fit the punishment."</P>
<P>Like the strike itself, workers took the lead in the filmmaking process. The screenplay was scrutinized, debated and voted on by mineworkers. Only a handful of professional actors, among them Will Geer and Mexican actress Rosaura Revueltas, had parts in the film. The bulk of the characters were played by workers themselves. So Local 890 President Juan Chac&oacute;n played himself, as did Clinton Jencks and his wife, Virginia, a leader in the women's auxiliary.</P>
<P>This was partly out of necessity, since the unions that represented professional actors and technical personnel--like the International Alliance of Theatrical Stage Employees and the Screen Actors Guild, led by raving anticommunist board member Ronald Reagan--banned members from working on the film. Blacklisted technical people were used, as were African Americans left out by the movie industry's Jim Crow hiring practices.</P>
<P>Those close to <I>Salt of the Earth</I> suffered. In 1954, Jencks was fined and sentenced to prison for perjury for signing the Taft-Hartley noncommunist affidavit. Revueltas was deported to Mexico and barred from returning.</P>
<P>McCarthyism would ruin the lives of an untold number of communists, unionists and other radicals--and virtually erase from the history books the fighting, left-wing tradition of the U.S. working class.</P>
<I><P>Salt of the Earth</I> should be watched today as a reminder of that hidden, left-wing tradition--and for its defiant message of standing up for what you believe in--a lesson that today's Hollywood machine should take heed of.</P>


