<P><font face="Times New Roman, Times, serif" size="5"><b>Maryland court imposes new moratorium</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mike Stark</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 21, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WEEKS AFTER Maryland's new Gov. Robert Ehrlich declared that he was restarting executions, a state court has halted them again.</P>
<P>On February 11, the Maryland State Court of Appeals stopped the planned March 17 execution of Steven Oken and agreed to hear his argument that the state's death penalty law is unconstitutional. The decision will halt all executions until this summer--effectively re-imposing Maryland's moratorium, declared last year by Ehrlich's predecessor, Parris Glendening.</P>
<P>Oken's attorneys argue that a recent U.S. Supreme Court decision changes the legal standard for evidence during the sentencing phase of a death penalty trial. Maryland currently requires juries to decide if there is a "preponderance of evidence" when considering whether a defendant should be put to death.</P>
<P>Oken's legal team says that the Supreme Court decision requires juries to make a stricter judgement--if the evidence is "beyond a reasonable doubt." If Oken wins his case, this would invalidate the death sentences of all 12 men on death row.</P>
<P>Anti-death penalty activists were elated at the news. They have spent the past several months organizing against Ehrlich's plan to lift the moratorium. Last week, opponents of capital punishment took their case to the state legislature, with representatives of four families of Maryland death row prisoners joining recently exonerated Illinois death row prisoner Madison Hobley in the state capital of Annapolis to demand a halt to executions.</P>
<P>They pointed to a recent study by the University of Maryland that shows how the death penalty is imposed in a racist and geographically arbitrary way in Maryland. "You can't have this study, and then just go ahead and execute," said Evangelist Bates, sister of death row inmate Vernon Evans.</P>
<P>Coming on the heels of legislation to abolish the death penalty proposed by Maryland's attorney general, the appeals court decision to halt Oken's execution is another sign that pressure from opponents of the death penalty is working. We've put a crimp in Ehrlich's plan to restart executions. It's time to step up the fight.</P>


