<P><font face="Arial, Helvetica, sans-serif" size="2"><b>AFGHANISTAN</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The CIA torturers</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 3, 2003 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"IF YOU don't violate someone's human rights some of the time, you probably aren't doing your job." That's the opinion of a U.S. official who supervised the capture and transfer of accused al-Qaeda members in Afghanistan.</P>
<P>In a December 26 article, the <I>Washington Post</I> detailed how CIA operatives in charge of the interrogation of suspected al-Qaeda and Taliban members at the Bagram air base in Afghanistan routinely use torture techniques.</P>
<P>The so-called "stress and duress" methods include keeping prisoners standing or kneeling for hours in hoods. "According to Americans with direct knowledge and others who have witnessed the treatment, captives are often 'softened up' by MPs and U.S. Special Army Forces troops," the <I>Post </I>reported. "The alleged terrorists are commonly blindfolded and thrown into walls, bound in painful positions, subjected to loud noises and deprived of sleep."</P>
<P>According to CIA officials, however, there's nothing wrong with that. It's all part of the agency's new "operational flexibilty."</P>
<P>In other cases, the CIA has been handing captives--along with a list of questions to be answered--to the intelligence services of countries such as Jordan, Egypt and Morocco, known for their routine use of more violent forms of torture. "We don't kick the shit out of them," said one U.S. official. "We send them to other countries so they can kick the shit out of them."</P>

