<P><font face="Times New Roman, Times, serif" size="5"><b>Georgia anti-abortion bigot's "grotesque" bill</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 3, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>EVEN HIS fellow anti-choice bigots think Bobby Franklin has gone a bit far. The Georgia lawmaker has submitted a bill to the state House of Representatives that would require any woman seeking an abortion to go through a trial and have a judge issue a death warrant for an "execution."</P>
<P>"This is probably the most grotesque bill we've seen," said Georgia Abortion and Reproduction Rights Action League spokeswoman Ebony Barley. "This would increase the risk to women--they would seek to get an abortion illegally."</P>
<P>Franklin's legislation is so outrageous that even some anti-choice groups are steering clear of it. Instead, they favor another proposal that would subject any woman seeking an abortion to a 24-hour waiting period and would present her with the so-called risks associated with the abortion procedure--including "possible detrimental psychological effects."</P>
<P>The proposal would make it harder for poor and working-class women to obtain an abortion. And the legislation even includes a provision to allow a "father" or "grandparent" to sue any physician that provided an abortion without the mandatory waiting period.</P>

