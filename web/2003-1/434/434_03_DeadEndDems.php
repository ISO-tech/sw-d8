<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The dead-end Democrats</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 3, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>JUST WHEN he was getting interesting for the first time in his political life. In early December, Al Gore announced that he wouldn't run for president in 2004.</P>
<P>Over the past several months, Gore seemed to be moving to the left, apparently positioning himself for another campaign. He was one of the only mainstream Democratic figures to pipe up with any criticism last fall when the Bush White House demanded a congressional blank check for a war on Iraq. And he recently began telling interviewers that he supported a single-payer health insurance system, along the lines of Canada's nationalized system.</P>
<P>Of course, this is a little late in the ballgame. In 2000, Gore hammered his only opponent in the Democratic primaries, former New Jersey Sen. Bill Bradley, for being a "big-spending" liberal--because Bradley talked about universal health care coverage.</P>
<P>Gore went on to run an incompetent campaign that was virtually indistinguishable from George Bush's--topped off with his failure to stand up to the Bush gang's theft of the White House in Florida. Still, this time around, Gore's newly discovered liberal positions would have opened up the discussion in mainstream politics--and given ordinary people more confidence to speak up.</P>
<P>With Gore gone, the early field of candidates for the Democratic nomination is almost entirely made up of cookie-cutter, Republican Lite senators and congresspeople--the same losers who rolled over for Bush again and again. This highlights the crisis facing the Democratic Party.</P>
<P>The Democrats went into last November's congressional elections with a lot going their way--from a slumping economy that Republicans had no answers for, to the growing questioning of Bush's war drive against Iraq. But the "party of the people" had so little to actually say to voters that the Republicans cruised to victory.</P>
<P>Since then, despite the media jabber about how Republicans are unstoppable, the Democrats have had opportunities to turn the tide. There was the breakdown of the White House's attempt to rig an independent commission investigation into September 11 by appointing war criminal Henry Kissinger as commission chair--after Kissinger resigned rather than reveal the international list of monsters that he peddles political influence for.</P>
<P>Then Trent Lott, the Senate majority leader-to-be, was caught declaring his admiration for Sen. Strom Thurmond's history of Ku Klux Klan-style racism. The Democrats should have had no trouble showing that Lott didn't say anything different from what prominent Republicans--like, say, Attorney General John Ashcroft--have been using as applause lines for years. Instead, Senate Majority Leader Tom Daschle spent the first days of the scandal explaining why Lott was a standup guy.</P>
<P>No one should rely on this bunch to stand up to Bush. After all, the Democrats serve the same corporate masters as the Republicans. If some Democratic presidential hopeful stakes out the liberal turf that Gore was exploring, it won't be on principle. The only way to win anything that we want from Washington is to organize for it--no matter which party is in charge.</P>

