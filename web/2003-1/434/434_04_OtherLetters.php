<p><a name="Israel"></a><font face="Times New Roman, Times, serif" size="4"><b>Attacked for opposing Israel</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Last year, after leaving a book release party in Greenwich Village, New York, for <I>The Struggle for Palestine,</I> I was attacked. </P>
<P>This happened as I was walking in the ritzy neighborhood of SoHo, looking for a subway station. People walking behind me noticed the back of my shirt, which read "End the Israeli Occupation." One of these goons picked up a good-sized rock and threw it at me, catching my right shoulder blade. He had the nerve to laugh and call me names. I was shocked. </P>
<P>Cheney and Bush hide behind the flag in an effort to "Americanize" the world. I urge those who are reading this right now to get all the information, use all the media you can and then make up your own mind. This government needs to go!</P>
<B><P>Dan Clemente, </B>New York City</P>

