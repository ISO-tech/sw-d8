<P><font face="Times New Roman, Times, serif" size="4"><b>Push for oil profits behind Bush's war</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><I><P>Socialist Worker</I> has done an excellent job analyzing and explaining the real motives behind Bush's coming war on Iraq. But a few additional points need to be made.</P>
<P>Firstly, the Bush administration has eyes not only on the conquest of Iraqi oil in the name of American oil companies, but also on the oil and natural gas of the Caspian Sea. Once America has gained control not only of Afghanistan but also of Iraq, the Caspian Sea region will be much more accessible. </P>
<P>Secondly, the Bush administration has largely turned a blind eye on Russia's ongoing war in Chechnya. In order for imperialism to gain full control of the Caspian, rebel insurgency has to be quelled.</P>
<P>Thirdly, the Bush administration recently has made much ado over the nuclear programs of the so-called "axis of evil" countries. Bush is already preparing the pretexts for a second, third, fourth and further wars.</P>
<P>Clearly, Bush's war will not stop with the conquest of Iraq. It will keep spreading until Washington controls the Middle East. For that reason, antiwar efforts must not stop in Washington or Boston, but must reach every community.</P>
<B><P>Bruce Burleson, </B>Roxbury, Mass.</P>

