<p><a name="unite"></a><font face="Times New Roman, Times, serif" size="4"><b>We need to unite to stop the right wing</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>The Republican landslide in the November election will give the GOP control of all three branches of our federal government. Since Bush is an ultra-conservative Republican and not a moderate, we can expect to see a shift toward the right in the U.S. political spectrum. </P>
<P>A Republican-dominated Congress will rubberstamp the Bush agenda, and the Democratic Party will be powerless to stop the Republican machine from steamrolling over them and commanding the direction of our country. </P>
<P>Democrats will remain closely aligned to the GOP during this "shift." They are afraid of taking a bold stance against a popular president and losing additional power. This means that the American public will not have a distinct alternative to the GOP. </P>
<P>In the absence of a distinct alternative, progressives, socialists, Greens and independents need to join forces. The fuel for our shift back towards the left can be found in the antiwar movement. By seizing the momentum of the antiwar movement, a new unified band of political third parties may emerge with a firm "leftist" position. </P>
<B><P>Robert DeCicco,</B> Colchester, Vt.</P>

