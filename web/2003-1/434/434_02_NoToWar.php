<P><font face="Times New Roman, Times, serif" size="4">Say no to Bush's war on Iraq</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Standing up to FBI harassment</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 3, 2003 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"THE FBI versus the People" was the title of the December 11 forum at the University of Massachusetts-Amherst against the FBI's questioning of an Iraqi professor and a union organizer.</P>
<P>Faculty, staff, students and community members began organizing in November after it was discovered that Musaddak Alhabeeb, an Iraqi-born American citizen, was questioned by an FBI agent and a UMass police detective about his political views, including his opposition to a war on Iraq.</P>
<P>Yaju Dharmarajah, a campus union organizer and citizen of Sri Lanka, says that police visited his home as well. When the officers were told that he wasn't there, they questioned his wife.</P>
<P>About 125 people attended the December forum. "It's a question of the FBI trying to intimidate us from fighting for social change," said Sara Lennox, a UMass professor. "We can't allow them to intimidate us."</P>
<P><font face="Times New Roman, Times, serif" size="4"><b>All out on January 18!</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>Antiwar activists on both coasts are making plans to get to Washington, D.C., and San Francisco on January 18 for national protests against the war.</P>
<P>Students at George Washington University (GW) and San Francisco State University (SFSU) have called for meetings of college and high school activists the day before. The two conferences will bring together elected representatives of antiwar groups--and hook up by telephone to share ideas and plan future events. For details on the West Coast conference, e-mail wcstudentconference@yahoo.com. For the East Coast, e-mail ecstudentconference@yahoo.com.</P>
<P>These demonstrations are a huge opportunity to build opposition to this war. All out January 18!</P>
<I><P>John Buttell contributed to this report.</P></I>

