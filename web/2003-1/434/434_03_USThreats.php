<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Threatening death and destruction around the world</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Washington is the real threat</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 3, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>BUILDING UP for a savage war on Iraq. Threatening a second war against North Korea. Backing coup-plotters in Venezuela. As the New Year began, Washington was once again using the "war on terror" as a cover to terrorize the world into doing its bidding.</P>
<P>Asked if the U.S. was ready to go to war against North Korea at the same time as Iraq, Defense Secretary Donald Rumsfeld blustered: "We're capable of winning decisively in one and swiftly defeating in the case of the other."</P>
<P>Democrats complained that the White House was downplaying North Korea's admission of its nuclear weapons program in order to focus on its war drive against Iraq. "This is a greater danger immediately to U.S. interests at this very moment, in my view," said Sen. Joseph Biden (D-Del.)</P>
<P>But neither Bush's threats against North Korea nor his relentless war drive against Iraq are about the threat of weapons of mass destruction, real or imagined. And the Iraq war isn't just about getting control of the world's second-largest oil reserves, either--important as that is to the Texas oilmen who run the White House.</P>
<P>These wars are about using September 11 to establish the doctrine of pre-emptive war against any government, anywhere, that gets in the way of the U.S. In some countries--like Venezuela--Washington will prefer to leave the dirty work to domestic opposition forces that will carry out policies to suit the U.S. But Bush wants to use September 11 as a blank check to wage war as well. </P>
<P>That's why Bush one year ago introduced the phrase "axis of evil" and included not just Iraq and North Korea, but also Iran--just weeks after that country backed Washington's war in Afghanistan.</P>
<P>Now, Bush will claim that North Korea's decision to expel international nuclear inspectors and restart the production of plutonium justifies the U.S.'s new aggressive foreign policy. But it's the other way around. North Korea was responding to U.S. threats--Bush's saber rattling and the U.S. decision to cut off fuel oil to the poverty-stricken country in October. And no one should doubt that the biggest threat of war comes not from North Korea but from the U.S. military's nuclear-armed 7th Fleet, which cruises off North Korea's shores.</P>
<P>Since the collapse of its old ally, the USSR, North Korea has been in steep economic decline and is hardly a threat to its neighbors. But Bush is determined to use this opportunity to push North Korea's rulers into a corner and to maximize U.S. leverage in the region--chiefly against China. That's why, nukes or not, there's no justification for Washington's war threats against North Korea.</P>
<P>Likewise, there's no excuse for a U.S. war on Iraq, even if United Nations (UN) inspectors find evidence--manufactured or real--of weapons of mass destruction. After all, one of Iraq's main sources of weapons before 1991 was Washington--which armed and funded Saddam Hussein to fight a war against Iran in the 1980s.</P>
<P>Like North Korea, Iraq is no threat to its neighbors, much less the U.S. And since no one seriously claims that Saddam has a connection with September 11, that wouldn't do as an excuse for war, either. So Washington has used weapons of mass destruction as a pretext for war instead--and bullied the UN Security Council into going along with the charade of inspections while the U.S. organizes its military buildup.</P>
<P>The growth of the antiwar movement shows that more and more people see through Washington's campaign of distortions and lies. We need to build the largest possible turnout in Washington, D.C., and San Francisco January 18--and organize opposition to every effort by Washington to impose its will on the world.</P>

