<P><font face="Times New Roman, Times, serif" size="5"><b>City councils take stand against Iraq war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 3, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>One cool October afternoon, well-known political activist A. Robert Kaufman picked up the <I>Baltimore Sun</I> and read about the city council in Santa Cruz, Calif., passing a resolution opposing a U.S. declaration of war against Iraq.</P>
<P>Inspired by the report, the 71-year-old Kaufman made some calls. One week later, Baltimore City Council member Kwame Abayomi introduced an antiwar resolution. By mid-November, more than 50 people attended a public hearing on the resolution, and one person testified against the measure.</P>
<P>Among those who testified in favor of the resolution were Eric Gustafson, a Gulf War veteran and executive director of the Education for Peace in Iraq Center in Washington, D.C.; Dr. Peter Molan, a retired U.S. Department of Defense Middle East analyst; and a Baltimore representative of Veterans for Peace.</P>
<P>On December 9, the city council passed the resolution with just one member abstaining--making Baltimore the second city in the state and one of more than a dozen U.S. cities with such a measure on the record.</P>
<P>Santa Cruz appears to have been the first to pass an antiwar resolution, approving its version on September 24. Five other California cities--Arcata, Berkeley, Oakland, San Francisco and Sebastopol--have done the same. Also, Carrboro, N.C.; Haines Township, Pa.; Ithaca, N.Y.; Kalamazoo, Mich.; New Haven, Conn.; Olympia, Wash.; Santa Fe, N.M.; Seattle; Takoma Park, Md.; and Washington, D.C., have passed their own resolutions.</P>
<P>Now, because of Kaufman's efforts, activists in Annapolis, Baltimore County, Harford County and Howard County are also working on persuading their elected officials to pass antiwar resolutions.</P>
<B><P>A.M. Sierra,</B> Baltimore</P>

