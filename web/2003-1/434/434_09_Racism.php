<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>How politicians use racism for political gain</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 3, 2003 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE CONTROVERSY over Trent Lott's praise for Strom Thurmond has exposed the ugly underbelly of U.S. politics. In the weeks following his statements, long ignored--but not unknown--parts of Lott's past came back to haunt him.</P>
<P>The Republicans ended up having to pitch Lott overboard. Yet had he not so publicly stuck his foot in his mouth, the birthday party for Thurmond would have passed with few noticing. And that says more about the political establishment's views on racism than all the outraged calls for Lott's resignation.</P>
<P>For decades, politicians like Thurmond and former Sen. Jesse Helms (R-N.C.)--for whom racism was the driving force behind their political careers--have been treated as mainstream fixtures in the Washington elite. Compare the bemused tolerance for these two racists with the derision and contempt that the establishment regularly expresses toward figures like Rev. Al Sharpton.</P>
<P>Helms' and Thurmond's retirements last year produced surprisingly few statements bidding them good riddance. If old segregationists like Thurmond and Helms get kid-gloves treatment, it's because powerful forces have decided that racism is tolerable after all.</P>
<P>For almost four decades, the Republicans have pursued a so-called "Southern strategy" aimed at capturing the remnants of Dixiecrats for their party. Using coded racial appeals against busing, affirmative action, "crime" and the like, Republican candidates helped rehabilitate the political uses of racism after the civil rights movement put open calls for white supremacy out of bounds.</P>
<P>That's why Bill Clinton was right to say that Lott's fellow Republicans were only condemning him because he "embarrassed them by saying in Washington what they do on the back roads every day." But Clinton shouldn't spend too much time on his high horse. Because he--and other "New Democrats" like him--played the racist card as well.</P>
<P>That's no accident either. Abandoning any notion of government action to correct racial injustice was central to the politics of the New Democrats from the start. At best, the Clinton-Gore administration promoted a "race-neutral" approach to social policy that tried to avoid issues of racial discrimination. At worst, it pandered to racism by scapegoating Black welfare recipients and Latino immigrants. </P>
<P>Clinton signaled his retreat on civil rights early on in his term when he abandoned Lani Guinier, his original choice to head the Justice Department's Civil Rights Division, in the face of a hysterical right-wing campaign branding Guinier a "quota queen." </P>
<P>While claiming that he wanted to "mend," rather than "end," affirmative action, Clinton ordered the actual end of dozens of federal affirmative-action "set-aside" programs. Clinton campaign operatives sabotaged the 1996 campaign against an anti-affirmative action ballot initiative in California. </P>
<P>The Clinton-Gore promotion of "personal responsibility" and harsh law-and-order policies fell disproportionately on African Americans and Latinos. And the administration refused to change federal drug sentencing laws on crack cocaine that overwhelmingly discriminate against Black offenders. </P>
<P>Over the last decade, the bipartisan retreat on issues of civil rights went hand-in-hand with the advance of the conservative agenda in both parties. After defeating Jim Crow, the civil rights and Black Power movements of the 1960s and '70s helped to push forward fights around class issues like expanded government protection for the poor, affordable housing and Medicare. The example of the civil rights struggle led many other oppressed groups, as well as sections of the labor movement, to demand their rights from employers and the government.</P>
<P>For bosses and conservatives who wanted to turn back this tide, using coded racism became a way to de-legitimize, divide and set back these struggles--as a prelude to a broader attack on workers. And all working people--Black and white--have suffered the consequences.</P>

