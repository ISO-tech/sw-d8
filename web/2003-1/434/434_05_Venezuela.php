<P><font face="Arial, Helvetica, sans-serif" size="2"><b>VENEZUELA</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The new push to topple Ch&aacute;vez</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 3, 2003 | Page 5</font></p></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE MONTH-OLD national "strike" in Venezuela is in fact an employers' lockout--and the latest Washington-backed effort to oust President Hugo Ch&aacute;vez.</P>
<P>While the lockout succeeded in dramatically cutting oil production by the government-owned oil company, PdVSA, this was due mainly to sabotage by top managers, technicians and oil tanker captains who oppose Ch&aacute;vez's ban on privatizing the oil industry, as well as his social reforms that benefit the poor.</P>
<P>Some groups of blue-collar workers have backed the strike, but many more have worked to repair sabotaged equipment and restore production. In reality, the so-called "strikers" are Venezuela's rich and corrupt oligarchy--more accustomed to corporate boardrooms, luxury villas and diplomatic suites than picket lines and demonstrations.</P>
<P>And according to the governor of Sucre, the oil-producing Venezuelan state, oil tankers owned by Philips Petroleum, ExxonMobil and other Western companies are backing the lockout by refusing to load oil for export. Such actions threaten to cripple Venezuela, the world's fifth-largest oil producer and the supplier of 13 percent of U.S. oil imports.</P>
<P>The opposition justifies its actions by claiming that Ch&aacute;vez is a dictator and a closet communist. In reality, he is a nationalist and populist in the tradition of Latin American governments of the 1930s and 1940s that used the state to carry out reforms. A career military man who led a failed coup in 1992, Ch&aacute;vez won the presidency seven years later. Since then, he has won five more elections and referendums by big majorities.</P>
<P>Ch&aacute;vez used oil revenues to create anti-poverty programs and established networks of supporters--called Bolv&aacute;rian Circles after Venezuela's founder--to mobilize support. Nevertheless, he has paid Venezuela's foreign debt and repeatedly made concessions to employers to try to avoid confrontation--for example, agreeing to hold a referendum on his government in August.</P>
<P>But this has only encouraged the opposition to demand more. In reality, Washington's  call for new elections last month was an endorsement of Ch&aacute;vez's opponents.</P>
<P>The last coup attempt in April, in which Ch&aacute;vez was briefly arrested, unraveled after just 36 hours when it was exposed as a bosses' plot. Opposition leader Pedro Carm&oacute;na, head of the employers' group Fedec&aacute;maras, immediately dissolved the National Assembly and excluded the anti-Ch&aacute;vez labor federation CTV from the government. Today, the opposition has put forward CTV President Carlos Ortega as one of its main spokespeople.</P>
<P>The opposition-dominated mass media and the Western press have played up demonstrations against Ch&aacute;vez, though they have been confined to wealthy areas in the capital of Car&aacute;cas. When three opposition protesters were killed during a demonstration last month, the accused gunmen turned out to be a Portuguese man suspected of being a provocateur. More provocations by the opposition are to be expected. </P>
<P>The longer the lockout continues, the more polarized Venezuela will become. Media opinion polls claim that Ch&aacute;vez's support is down to 30 percent--although it's difficult to verify such claims. On the other hand, the widespread hatred of the coup plotters shows the potential for a popular mobilization and a struggle for change far more radical than anything Ch&aacute;vez has attempted. </P>
<P>Already, the failed April coup has led to massive growth in the Boliv&aacute;rian Circles and popular demands for action against the employers and coup plotters. But Ch&aacute;vez's hesitations and compromises threaten to undermine the struggle against the right.</P>
<P>The key to defeating a new coup isn't a deal between Ch&aacute;vez and the opposition, but a mobilization of the poor and the organized working class against the right wing and its wealthy backers.</P>

