<P><font face="Arial, Helvetica, sans-serif" size="2"><b>OBITUARY</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Philip Berrigan</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P><B>Dear <I>Socialist Worker,</I></B></P>
<P>The death of Philip Berrigan in early December, while sad, also reminded me of why a life of commitment for social justice can be an inspiration.</P>
<P>Philip and his brother Daniel were radical pacifists who tried to act consistently on their moral beliefs. I remember in 1967, when then-Rev. Philip Berrigan and three other pacifists went into a Baltimore draft board office, opened files and poured in a mixture of their own and animal blood, destroying hundreds of records. "This blood is nothing compared to the blood being shed in Vietnam," Philip told reporters.</P>
<P>Another time, he helped break into another draft board and burned thousands of records with homemade napalm--the jellied gasoline used against the Vietnamese. Philip helped found the Plowshares Movement, a loosely organized collective credited with more than 80 acts targeting military installations and war-making equipment. Most of these acts landed him in jail. </P>
<P>His last act of civil disobedience was in December 1999, when he joined three others in an "assault" on National Guard A-10 Thunderbolts (Warthogs) stationed in Maryland. These aircraft fire depleted uranium shells at the rate of 3,900 per minute. Depleted uranium is extremely toxic and has been linked to birth defects, liver and kidney diseases, memory loss and cancers in U.S. military personnel as well as uncounted numbers of Iraqis after the first Gulf War.</P>
<P>As Philip often said, "War is the government's number one business. My business is to commit acts of peace." While Berrigan's commitment was unquestioned, his approach was largely symbolic--and therefore was limited in essential respects. Only the most committed could join his actions. </P>
<P>From his support for civil rights in the 1950s to opposition to the current push for a war against Iraq, Philip Berrigan devoted his life to social justice--and many took inspiration from that commitment.</P>
<P><B>Bill Roberts,</B> Chicago</P>

