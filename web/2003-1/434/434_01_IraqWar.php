<P><font face="Times New Roman, Times, serif" size="4">U.S. buildup for war on Iraq</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>They don't care how many die</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 3, 2003 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ACTIONS SPEAK louder than words. The Bush administration may claim that it hasn't yet decided whether to attack Iraq. But as <I>Socialist Worker</I> went to press, the Pentagon was ordering dozens of strike aircraft and two more aircraft carrier battle groups to the Persian Gulf--and doubling its deployment of troops.</P>
<P>The countdown for a devastating war--on an already devastated country--has started. U.S. officials and their media mouthpieces are already hyping the accuracy of their precision, laser-guided "smart" bombs--which they soon will be dropping in massive numbers on Iraq. "We can service an incredible number of different aim points in a very short amount of time," gushed Navy Capt. Mark Fox, who's now stationed in the Gulf.</P>
<P>Tell that to Mohammed Sharif Reda's family. In early December, Mohammed--a 23-year-old mechanic who was married two months earlier--was slammed to the ground and cut to ribbons by metal shards from a U.S. bomb. Three other people--all of them civilians--died in the same attack, which destroyed the offices of an oil company in the center of Basra, Iraq's second-largest city.</P>
<P>Naturally, the Pentagon claims that U.S. warplanes patrolling the so-called "no-fly zones" over northern and southern Iraq fired their weapons at a "military target"--in the latest in an escalating series of attacks. But like so many times before, the bombs missed--and Mohammed paid the price.</P>
<P>The human toll of the coming war will be enormous. Aid agencies predict that an assault will produce a flood of 1 million refugees and disrupt the fragile distribution network for food aid that currently supports 16 million Iraqis--more than two-thirds of the population.</P>
<P>Washington says that this war is about the threat posed by Iraq's weapons of mass destruction. But no one should be in any doubt that the U.S. has its eye on something else: Iraq's oil.</P>
<P>Washington's apologists ridicule this idea, of course. Oil "has barely been on the administration's horizon in considering Iraq policy," said Patrick Clawson, an analyst with the Washington Institute for Near East Policy, in October. "U.S. foreign policy is not driven by concern for promoting the interests of specific U.S. firms."</P>
<P>But in 1999, Clawson--whose think tank has a cozy relationship with the Bush administration--was singing a different tune at a Capitol Hill forum on a post-Saddam Iraq. "U.S. oil companies would have an opportunity to make significant profits," he said. "We should not be embarrassed about the commercial advantages that would come from a re-integration of Iraq into the world economy."</P>
<P>In pursuing oil profits, the U.S. has no problem sacrificing blood--Iraqi and American. We have organize to stop this grab for oil and power. No war on Iraq!</P>

