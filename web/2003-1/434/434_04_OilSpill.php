<P><font face="Times New Roman, Times, serif" size="4">Tanker owners' scam to blame for spill off Spain</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Deregulation disaster</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 3, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Two million gallons of oil poured out of the oil tanker <I>Prestige</I> when its hull broke during a storm November 19 and devastated the once-beautiful Galician Coast of Spain. Locals now call the coast, once known for its wildlife and fishing industry, the "Coast of Death." It's covered in black sludge. </P>
<P>Was this spill an inevitable consequence of transporting large amounts of dangerous material from one part of the world to another? No. </P>
<P>One of the ugliest faces of globalization in the shipping industry is a practice called "flags of convenience." Cash-poor countries--notably the Bahamas, Liberia, Panama and Cyprus--allow shipping companies to register in their countries and fly their flags to escape regulation. The <I>Prestige,</I> for example, was built in Japan, owned by a Liberian-registered firm, registered in the Bahamas, chartered by a Swiss-based Russian oil trader and managed by Greeks. </P>
<P>Flag-of-convenience ships don't answer to the regulation laws of the countries of their owners--and are in practice not really held accountable by any country. About 55 percent of the oil tankers in the world today are flag-of-convenience ships. The owners disregard safety concerns, assuming that they can get away with anything. So far, for example, only the Greek captain of the sunken ship has been punished, receiving jail time in Spain.</P>
<P>In addition to environmental disasters caused by the lack of regulation, flag-of-convenience ships escape minimum-wage and labor safety laws. Unsafe conditions, long hours and disgustingly low pay are common for the roughly 360,000 workers on these ships. The workers are usually not represented by seafarers' unions because they fall outside the national "jurisdiction" of any one union. </P>
<P>The disaster in Spain is one more example of how deregulation for the sake of profits is not only ruining our planet with lightning speed--and stepping all over workers' rights and safety to do it. </P>
<B><P>Sarah Knopp,</B> San Pedro, Calif.</P>

