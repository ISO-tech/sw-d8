<P><font face="Arial, Helvetica, sans-serif" size="2"><b>NORTH KOREA</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Rumsfeld escalates the confrontation</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>What's behind U.S. war threats?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By David Whitehouse</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 3, 2003 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH administration's confrontation with North Korea reached a new level of crisis last week after the expulsion of United Nations (UN) nuclear inspectors. The expulsion order came in response to new threats of war from Defense Secretary Donald Rumsfeld, who a few days earlier declared that the U.S. "could fight and win two wars at once."</P>
<P>Rumsfeld's saber rattling made it clear that the prospect of a U.S. military conflict with North Korea is real. But North Korea would be capable of inflicting heavy casualties on U.S. troops stationed in South Korea, and on cities in the South. That's why the administration has seemed to adopt a strategy of strangulation--possibly to include UN sanctions--rather than direct combat.</P>
<P>In November, the U.S., Japan and South Korea cut off fuel oil shipments to the impoverished country, where per capita income is less than $2 a day. That followed North Korea's admission in October that it had violated a 1994 agreement with the U.S. by collecting enriched uranium to make nuclear weapons. The UN inspectors were expelled from the Yongbyon reactor complex, which can produce plutonium for nuclear weapons. </P>
<P>North Korea mothballed the complex as part of the 1994 agreement. Since mid-December, however, the government has taken steps to reactivate it, saying that the halt in fuel oil shipments nullified the earlier agreement--and forced them to generate energy with nukes to make up the shortfall.</P>
<P>The board of governors of the International Atomic Energy Agency, the UN's nuclear watchdog, scheduled an emergency meeting for January 6--where it is likely to recommend that the UN Security Council impose further economic punishment on North Korea for violating the nuclear Non-Proliferation Treaty.</P>
<P>Meanwhile, the UN's World Food Program warned that it would be unable to reach 2.9 million vulnerable North Koreans this winter unless Japan or the West makes immediate contributions. Although commentators often talk about North Korea's "unpredictable" behavior, both crises--today's and the one in 1993-94--arose from U.S. military threats.</P>
<P>North Korea's pursuit of nuclear arms in 1993 came after Bill Clinton decided to renew massive war games in South Korea that his predecessor, George Bush Sr., had abolished. Clinton also had announced that nuclear missiles previously aimed at the ex-USSR would be re-targeted on North Korea.</P>
<P>The last few months of crisis follow George W. Bush's State of the Union address early last year, where he declared that North Korea was part of an "axis of evil," along with Iraq and Iran. Then, in March, a leaked "nuclear posture review" confirmed that the Bush White House had formalized a decades-old policy of planning for the use of nuclear weapons against North Korea even without provocation.</P>
<P>As in 1993-94, North Korea responded to the threats by advertising its willingness to develop a nuclear deterrent. The government hopes to use this to negotiate an aid package and non-aggression agreements.</P>
<P>China and Russia, both members of the UN Security Council, have blamed Bush for the crisis. Even more worrying for Bush is the growth of resistance to U.S. policy in South Korea, where 37,000 U.S. troops are stationed.</P>
<P>Resentment of the U.S. presence peaked on the weekend before Christmas as tens of thousands joined vigils across South Korea to protest the acquittal of two U.S. soldiers whose armored vehicle crushed two Korean schoolgirls to death in June.</P>
<P>South Korea's president-elect, Roh Moo-hyun, who takes office February 25, has called on North Korea to reverse its nuclear weapons program. But unlike many previous South Korean leaders, Roh is no U.S. stooge. A week before defeating a pro-Washington candidate in the December 19 presidential election, Roh said that South Korea should be neutral in a military confrontation between the U.S. and the North.</P>
<P>Roh, as well as outgoing President Kim Dae-jung, are opposing the intervention of the UN Security Council in North Korea, which they fear would sideline South Korea--the country most threatened by North Korea's weapons--in the resolution of the dispute.</P>

