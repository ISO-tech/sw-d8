<p><a name="Rancho"></a><font face="Times New Roman, Times, serif" size="4"><b>Rancho Los Amigos Medical Center</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Cesar Montufar</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LOS ANGELES--Activists have built a tent city and are holding an around-the-clock vigil to keep Los Angeles County from closing down an important medical center.</P>
<P>Rancho Los Amigos Medical Center treats people with a range of injuries and medical conditions affecting the spinal cord. The Los Angeles County Board of Supervisors plans to vote on June 30 to shut the center down. The center sees between 3,000 and 4,000 patients a year and is the only hospital of its kind west of the Mississippi River.</P>
<P>During the vigil, one wheelchair-bound speaker from neighboring Orange County explained that he can't find the help he needs near his home, and another speaker named Rico said that he had traveled nearly 500 miles from the Bay Area city of San Jose. "Rancho helped me to be independent," Rico said in Spanish. "I don't know what people like me without insurance would do without it." </P>
<P>For many protesters, the fight to keep the center open is directly connected to organizing against the war on Iraq. Activist Ami Motevalli explained that a Bush administration official who visited the center commented about how "he recognized that we need these facilities and said it'll be a shame to close them. Then a week later," she continued, "they ask for $75 billion for their war!"</P>
<P>Pablo Murillo, one of the hospital workers and a member of Service Employees International Union Local 660, reflected on the priorities of the system. "This government is our enemy," said Murillo. "They complain about the high crime rates in the 'ghetto,' but they want to take away the only resources the people in the ghetto have."</P>


