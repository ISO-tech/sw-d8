<P><font face="Times New Roman, Times, serif" size="4"><b>New York City hospitals</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Thomas Barton and Michael Ware</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Mayor Michael Bloomberg wants to overcome the city's $5 billion budget deficit by laying off 4,000 public hospital workers. In response, AFSCME District Council (DC) 37 staged rallies in front of New York public hospitals March 27--with "staged" being the operative word. </P>
<P>At Bellevue Hospital, a DC 37 local officer told <I>Socialist Worker</I> that DC 37 officials told him not to turn out the workforce. "They said they didn't have a permit, so they didn't want too many people showing up," said the local officer. "Then, at the last minute, they called me and said, 'Get some people out there.'" </P>
<P>About two dozen hospital workers stood looking on while DC 37 big shots gave quotes to the press and sound bites to local TV reporters. But hospital workers are looking for a better answer to Bloomberg than good press. "They should get rid of the big administrators that sit there all day and do nothing but stress people out," Elsa Felix from AFSCME Local 1549 told <I>Socialist Worker</I>. </P>
<P>So far, DC 37, which represents 125,000 city union workers, has disgusted members by promising to fight hard "for better severance benefits" and promising that if they succeed in electing Democrats in 2004, everything will be fine. Meanwhile, Bloomberg started the wholesale firing of DC 37 members January 1 without the slightest response from the union leadership, other than whining press releases. </P>
<P>Feeling the heat from below, DC 37 this week called for a mass demonstration at City Hall April 29. Whether this will be more than another photo-op and sound bite show remains to be seen. The DC 37 membership, as usual, will have to do the heavy lifting. </P>
<P>We can invite every student whose education is being cut, and every citizen who depends on public hospitals and other threatened city services. We can reach out to every worker outraged by mass layoffs and cuts in city services while money is wasted for Bush's imperial war. </P>
<P>As Ralph Palladino, second vice president for AFSCME Local 1549, put it, "They're taking money that should be going to health care in order to pay for the war. That should be common sense by now."</P>

