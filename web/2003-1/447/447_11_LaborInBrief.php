<p><a name="Tyson"></a><font face="Times New Roman, Times, serif" size="4"><b>Tyson</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Robin Gee</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>JEFFERSON, Wis.--Despite cold and snow, more than 300 people rallied March 29 outside the Tyson plant gate to support striking workers. </P>
<P>The workers walked out February 28 when they refused to accept a contract with huge concessions. The mood was upbeat but determined. </P>
<P>Tyson has attempted to recruit scabs from Milwaukee--a 90-minute drive--by offering free transport and lodging, but so far they've only managed to round up four or five workers this way. According to reports from inside, the plant is running far under capacity. </P>
<P>All this--along with tremendous community support--has given encouragement to the strikers. The union is organizing "truth tours" to carry news of their struggle across the country, especially in meatpacking areas of Iowa, Minnesota, Nebraska and Nevada.</P>
<P>Recently, strikers picketed Qualitemps in Milwaukee for supplying scabs, and they are targeting Rightway bus service, normally a Midwest school bus service, for agreeing to transport scabs.</P>
<P>Solidarity with the Tyson strikers!</P>

