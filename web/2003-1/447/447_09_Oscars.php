<P><font face="Times New Roman, Times, serif" size="4">Oscars producers couldn't censor the stars</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Hollywood takes a stand against war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sue Fitzgerald</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 4, 2003 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THIS YEAR, as bombs fell on Baghdad, the Oscars rolled up the  red carpet and toned down the glitz. Rather than throngs of adoring fans, celebs were greeted by antiwar protesters.</P>
<P>Antiwar sentiment could be found inside the Oscars as well, as several stars voiced opposition to George W. Bush's war on Iraq. Mexican actor Gael Garcia Bernal, introducing the best song nomination from <I>Frida</I> said, "If Frida [Kahlo] were alive, she'd be on our side, against war."</P>
<P>Several Oscar winners, including Spanish director Pedro Almod&oacute;var, made antiwar comments or wore pins symbolizing their opposition to war. But by far, the highlight of the night was left-wing filmmaker Michael Moore, who won for <I>Bowling for Columbine</I>.</P>
<P>In a show of solidarity, all of his fellow nominees joined Moore on stage as he angrily blasted Bush. "We live in a time when we have fictitious elections that elect a fictitious president who sends us to war for fictitious reasons," Moore said. "We are against this war, Mr. Bush! Shame on you, Mr. Bush!"</P>
<P>As the mike was lowered, the orchestra began to play and several boos were heard. You could hardly hear Moore comment, "Any time you have the pope and the Dixie Chicks against you, your time is up!"</P>
<P>Afterward, the press picked out Moore, because he made the strongest antiwar statement, and claimed that politics had no place at ceremony celebrating the arts. This backlash started before the Oscars. In the weeks before, anti-Hollywood Web sites sprang up, demanding antiwar actors be silenced or put out of work. And it seems the Hollywood bosses are listening.</P>
<P>Sean Penn, who visited Iraq in December, says he lost a job after he wrote an open letter to Bush opposing the war. Martin Sheen says that top NBC executives "let it be known they're very uncomfortable" with his antiwar stance. The Oscars producers even came up with a blacklist of celebrities who would not be allowed to present awards, including George Clooney and Meryl Streep.</P>
<P>Although many were ultimately allowed to participate, they were ordered not to stray from the script. Some of them refused to be silenced and made their opposition known, although none as sharply as Moore.</P>
<P>The current backlash is frighteningly reminiscent of the McCarthy-era witch-hunts that destroyed the lives of many left-wing artists in the 1950s. Fortunately, some forces have denounced the attacks, including the Screen Actors Guild, which issued a statement condemning "even a hint of a blacklist."</P>
<P>Still, right wingers like "journalists" Bill O'Reilly and Rush Limbaugh (celebrities themselves!) whine that stars should leave politics to the "experts"&#133; like Bush. They're quick to forget that three years ago candidate Bush--unable to name, among others, the leaders of India or Pakistan--flunked a reporter's foreign affairs quiz. Some "expert."</P>
<P>Bush and his "experts" have unleashed the power of the U.S. military on innocent Iraqis. Tens of thousands will likely be killed. Millions more lives will be wrecked. You don't need to be an "expert" to know that this war is about oil, and about increasing U.S. world dominance.</P>
<P>All opposed to these warmongers, including celebrities, must add their voice to the growing antiwar movement.</P>


