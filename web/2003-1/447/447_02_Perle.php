<P><font face="Times New Roman, Times, serif" size="4">Loud-mouth hawk demoted</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Selling influence at the Pentagon</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 4, 2003 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>IRAQ WAR hawk Richard Perle last week resigned as chair of the Defense Policy Board, the powerful panel that advises the Pentagon.</P>
<P>Perle will remain on the board, but he quit as chair after coming under fire for his cozy connections to corporations looking for favors from the Pentagon. The bankrupt telecommunications giant Global Crossing agreed to pay Perle $725,000 if the Defense Department approved the sale of the company to Asian investors.</P>
<P>Perle's resignation is also an expression of the unraveling U.S. military strategy in Iraq, which he helped author.</P>
<P>Perle isn't the only one straddling both sides of the so-called military-industrial complex. At least nine of the Defense Policy Board's 30 members "have ties to companies that have won more than $76 billion in defense contracts in 2001 and 2002," according to the Center for Public Integrity.</P>


