<P><font face="Times New Roman, Times, serif" size="5"><b>Union leaders bow to concessions at American</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 4, 2003 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AMERICAN AIRLINES is using the threat of bankruptcy to try to extract concessions worth $1.8 billion per year from its three main unions--and labor leaders are going along with the plan.</P>
<P>The proposed deal will cut the pay of mechanics by 17.5 percent and ramp workers by 16 percent--and includes changes on work rules to force employees to be more productive even as their wages shrink. Both groups of workers are represented by the Transport Workers Union (TWU), whose members will give up $620 million per year.</P>
<P>The Air Line Pilots Association agreed to $660 million in cuts, while officials of the Association of Flight Attendants accepted $340 million in cuts. The deal is subject to approval by the rank and file, and unions plan to complete voting on the agreement by April 15.</P>
<P>But the deal won't guarantee that American will avoid bankruptcy, which United Airlines and US Airways have used to push for even greater concessions. US Airways emerged from bankruptcy March 31--but not before imposing an additional 5 percent pay cut on workers because of "uncertainty" caused by the war. Those cuts come on top of $1 billion in concessions the company got from unions since last August.</P>
<P>In return for taking the cuts, the unions got a 31.2 percent share of the company's ownership. But the federal government's Air Transportation Stability Board (ATSB) also got a 10 percent share in exchange for guaranteeing a $1 billion loan to the company.</P>
<P>The ATSB is a driving force in getting concessions from labor. Earlier this year, the board forced United Airlines into bankruptcy because its proposed wage and benefits cut weren't steep enough. United then used bankruptcy to impose cuts in pay and benefits--and is threatening to ask a bankruptcy court judge to tear up the union contracts altogether. Never mind that workers supposedly own the company under an employee stock ownership plan.</P>
<P>Airlines are using the war to impose cuts--including 10,000 layoffs since the U.S. attack on Iraq began--just as they used the September 11 attack to slash payrolls. The real problem isn't labor costs, but deregulation, which has put the system on a crazy boom-and-bust cycle that's left too much capacity for too little business, the <I>New York Times</I> pointed out March 29. </P>
<P>Their solution? More labor concessions--and perhaps the liquidation of United Airlines and the elimination of its 72,000 jobs. Concessions only lead to more concessions--in the airlines and every other unionized industry.</P>
<P>Following the logic of concessions to restore profits threatens to wipe out decades of union gains. American Airlines workers should vote "no" on the proposed deal--and start organizing for a fight to defend their jobs.</P>


