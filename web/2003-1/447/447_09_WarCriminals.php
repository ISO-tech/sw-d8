<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Who are the real war criminals?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 4, 2003 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE BUSH administration's threats to try various Iraqi officials as "war criminals" have been increasing in intensity as the war goes on. "It is against the Geneva Convention," Donald Rumsfeld intoned after captured U.S. soldiers appeared on Iraqi television, "to show photographs of prisoners of war in a manner that is humiliating for them."</P>
<P>Claiming that U.S. prisoners had been executed, Bush announced, "We expect such war crimes, but we will not excuse them." Others complained about Iraqis not "fighting fairly."</P>
<P>This is the crassest hypocrisy. Iraqis who resist the U.S. invasion are labeled "war criminals." But for the world's most powerful army, bent on conquest, any crime is excused.</P>
<P>The U.S. is holding more than 600 prisoners from the Afghanistan war at Guant&aacute;namo Bay in Cuba. According to British journalist George Monbiot, the U.S. has violated 15 articles of the Geneva Convention at Guant&aacute;namo, including holding prisoners indefinitely even after "cessation of hostilities." Twenty-five prisoners have attempted suicide.</P>
<P>The U.S. gets around the problem by simply reclassifying these prisoners of war as "unlawful combatants." The U.S. also routinely tortures prisoners in its Bagram prison in Afghanistan. According to a <I>New York Times</I> report, as many as 100 prisoners were "made to stand hooded, their arms raised and chained to the ceiling, their feet shackled, unable to move for hours at a time, day and night," completely naked and "deprived of sleep for days on end." Lt. Gen. Daniel McNeill, the U.S. commander of coalition forces in Afghanistan, explained that this treatment is "in accordance with what is generally accepted as interrogation techniques."</P>
<P>Reports of U.S. forces bombing and shooting Iraqi civilians and cutting off their water and food make it clear who the war criminals are. "The Iraqis are sick people, and we are the chemotherapy," said Corp. Ryan Dupre, in response to unexpectedly fierce resistance. "I am starting to hate this country. Wait till I get hold of a friggin' Iraqi. No, I won't get hold of one. I'll just kill him."</P>
<P>What's known as the "Geneva Convention" was established by international agreement at three conferences held in 1907, 1929 and 1949. These "rules of war" were compiled by representatives from the world's great powers--who then proceeded in the years that followed to engage in mutual slaughter that cost the lives of something like 60 or 70 million people, most of them civilians.</P>
<P>The Geneva Convention does indeed prohibit the mistreatment of prisoners of war. It also prohibits pillage and bombing of civilians, and the use of poison gas and "arms calculated to cause unnecessary suffering."</P>
<P>All of these rules were routinely flouted in both world wars by all sides. The U.S., as the world's biggest power, is naturally the biggest flouter. The U.S. killed more than 2 million Vietnamese in its war against Vietnamese independence--by carpet bombing civilian areas and carrying out systematic massacres. But no U.S. official has ever been tried for war crimes, because powerful nations created tribunals to punish their enemies, not dispense justice. Their own war crimes are simply justified as "military necessity"--one of the Geneva Convention's deliberate loopholes.</P>
<P>The Geneva Convention also bans civilians from taking hostile action against an enemy. French resistance to Nazi occupation during the Second World War was a war crime. So are the actions of Iraqi civilians who protect their homes against U.S. troops.</P>
<P>But complaints about Iraqis not "fighting fair" boil down to the conqueror wishing the conquered to give in peacefully. We want to minimize your deaths, goes the U.S. argument, but since you won't accept "liberation," and since you insist on resisting us, instead of greeting us with flowers, we have brought along M-16s, Abrams Tanks and cruise missiles to kill you.</P>

