<p><font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>BACKGROUND AND ANALYSIS</b></font></p>

<P><font face="Times New Roman, Times, serif" size="3"><b>Answering a question that faces the antiwar movement</b></font><BR>
<A HREF="447_08_SupportTroops.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Should we "support our troops"?</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">The Bush administration's rallying cry to "support our troops" is nothing more than a public relations tool to gain approval for an unpopular war.</P>

<P><A HREF="447_06_MiddleEastHistory.shtml"><font face="Times New Roman, Times, serif" size="4"><b>The Middle East fight for freedom</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">Bush and his hawkish advisers believe that the U.S. war on Iraq is the first step to redrawing the map of the Middle East. They are following a long line of imperial powers that have meddled in the region.</P>

<P><A HREF="447_09_WarCriminals.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Who are the real war criminals?</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">Iraqis who resist the U.S. invasion are labeled "war criminals." But for the world's most powerful army, bent on conquest, any crime is excused.</P>

<P><A HREF="447_06_Israel.shtml"><font face="Times New Roman, Times, serif" size="4"><b>Is this a war for Israeli interests?</b></font></A><BR>
<font face="Times New Roman, Times, serif" size="3">Is the U.S. war on Iraq driven by Washington's support for Israel? Does Israel have more to gain from this war than the U.S.? Some opponents of the war--from both right and left--wrongly answer "yes" to these questions.</P>

