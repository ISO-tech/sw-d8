<font face="Times New Roman, Times, serif" size="5"><b>Stop this war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 4, 2003 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"I GIVE this pledge to the citizens of Iraq: We're coming with a mighty force to end the reign of your oppressors, " George W. Bush said on March 31. That same day, Bush's "mighty force" unleashed some of the heaviest bombing of the war in Baghdad.</P>
<P>And in the southern Iraqi town of Najaf, U.S. soldiers killed seven women and children and wounded two others by firing on a van at a checkpoint. This was the latest in a series of horrific killings of innocent civilians by U.S. and British forces across Iraq.</P>
<P>Far from being welcomed, the Washington-run "coalition" has faced fierce resistance in every Iraqi city that it has tried to enter. After a house-to-house battle in the town of Nasiriya, U.S. Marines were opening fire on every civilian vehicle moving in the area.</P>
<I><P>Times</I> of London journalist Mark Franchetti described the deadly result: "[A] truck came down the road. Again, the Marines fired. Inside, four men were killed. They had been travelling with some 10 other civilians, mainly women and children, who were evacuated, crying, their clothes splattered in blood. Hours later, a dog belonging to the dead driver was still by his side."</P>
<P>U.S. and British forces have carried out such indiscriminate killing virtually everywhere that they've operated in Iraq. Is it any wonder that most Iraqis see these soldiers not as liberators, but as oppressors? The reality is that the invaders have only "liberated" the oilfields of southern Iraq. And they are shooting--and starving--the Iraqi people into submission.</P>
<P>Yet in their drive to conquer Iraq for oil and empire, Washington is preparing still more butchery--and on a vast scale. An anonymous U.S. military official told reporters that the Pentagon is prepared to pay a "very high price" in U.S. casualties to conquer Baghdad--comparing the operation to battles in the Second World War when the U.S. lost 1,000 troops a night.</P>
<P>And for every American killed, the U.S. is planning to slaughter many times the number of Iraqis. We need to mobilize now to stop this criminal war.</P>

