<p><a name="legacy"></a><font face="Times New Roman, Times, serif" size="4"><b>King's legacy of nonviolent struggle</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Regarding Alan Maass's article on Martin Luther King (<I>SW,</I> February 21), I offer a few brief observations. </P>
<P>First, I disagree that "King's principle of nonviolence couldn't be sustained in the face of [the South's] &#133;violence." In practice, activists did not resort to "armed self-defense." Instead, a movement that took almost 15 years to build was crushed when it devolved into riots. The fact is that King (and ironically Malcolm X as well) recognized that a minority of people, even heavily armed, cannot "overthrow" the majority by force. </P>
<P>I further disagree that King "played a central role in toning down" student civil rights leader John Lewis during the 1963 march. King had a subordinate role in the march, as it was mainly organized by A. Phillip Randolph. Yes, Lewis was censored; yes, King could have voiced more opposition. But the censoring was accomplished by John F. Kennedy, not King. </P>
<P>I agree that socialists must "claim Martin Luther King as an inspiration." We must further embrace his concept of militant nonviolence. Violence only begets more violence. I, for one, feel socialists would make more headway by going in the direction of King's Poor People's Campaign.</P>
<B><P>David Bliven,</B> Oceanside, N.Y.</P>

