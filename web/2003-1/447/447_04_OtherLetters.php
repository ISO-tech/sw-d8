<p><a name="Priced"></a><font face="Times New Roman, Times, serif" size="4"><b>Priced out of an education</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Rulers in Washington continue their current assault on the home front by slashing the budget to make room for their unjust war. </P>
<P>At Northeastern University, the administration has seen families struggling to send their kids to college, and has agreed to increase "financial aid" for students in need. Their proposed solution? Accept fewer students--and have the ones who do make it in pay a higher tuition! The new selectivity standards are based on SAT tests, where it has been shown that the better funded your high school, the better your chance of a higher score. </P>
<P>When asked if he felt financially secure, one Northeastern student replied, "I don't want to take out another loan like this year. However, it looks like I'm going to have to."</P>
<P>Senior vice president of administration Larry Mucciolo recently justified the new changes in a way that would have made White House spokesman Ari Fleischer proud. Mucciolo said the money would help more students in financial need. In other words, charge students more--so that they can get more financial assistance. </P>
<P>These changes are designed to allow more kids of the ruling class in, while leaving the rest of us to beg for money from a bank where we will be trapped in debt.</P>
<B><P>Ben Larrivee,</B> Boston</P>


