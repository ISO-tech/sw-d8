<P><font face="Times New Roman, Times, serif" size="4"><b>Credit card sharks circle for the kill</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>It seems that as our debt-driven economy continues to sputter, some companies are being driven to more desperate measures to try to collect money from consumers. </P>
<P>The other day I came home and found the following message on my answering machine: "This message is for Evan Kornfeld. I'm calling in reference to Anton Dinckelacker [not his real name], a neighbor who lives in apartment 111. If you happen to know him, you can give him a message to call Helen at Robinsons-May [a department store chain]..."</P>
<P>I try to picture in my mind what would happen if I were to actually relay this message to the inhabitant of apartment 111: "Dude," I say to him. "Helen from Robinsons-May asked me to tell you to call her right away. Sounds like you owe them some money or something."</P>
<P>"Whoa!" he says. "I must have forgotten to make a payment on my store credit card that charges 20 percent interest. If only the phone company hadn't disconnected me for not being able to pay my bills, I'm sure Helen would have contacted me herself. Thanks, man, for relaying this important message to me."</P>
<P>"Aw, it's nothing," I say with unfeigned modesty. "You should thank Robinsons-May for getting total (and unpaid) strangers to nag people to pay their debts."</P>
<P>I must confess, however, that I failed to perform my "civic duty." Obviously, debt collectors can't rely on people like me, so I wonder what methods they may have to try in the future to hunt down delinquent debtors. </P>
<P>Perhaps they can recruit the Boy Scouts or the Young Republicans. Given the inventiveness and determination of these capitalists, I'm sure they won't fail to surprise us.</P>
<B><P>Evan Kornfeld,</B> Los Angeles</P>

