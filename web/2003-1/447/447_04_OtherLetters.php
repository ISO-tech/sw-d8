<p><a name="Hollywood"></a><font face="Times New Roman, Times, serif" size="4"><b>Hollywood unions oppose witch-hunt</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>The Screen Actors Guild (SAG) released a statement on March 3 condemning any hint of a blacklist for performers who have spoken up against Bush's war drive.</P>
<P>The statement also spoke about the sordid history of blacklisting in the entertainment industry, mentioning the shameful McCarthy-era witch-hunts that ruined the lives and careers of many of Hollywood's most talented artists.</P>
<P>The statement was also supported by IATSE, the technician's union for entertainment workers. These statements are important, since both unions have a history of collaborating with blacklists. </P>
<P>There is also now a disturbing trend of some pundits openly calling for a return to the days of the blacklists, suggesting that those with "inappropriate views" (opposition to Bush's war) be punished by not allowing them to work. </P>
<P>As the statement itself concludes: "With a painfully clear appreciation of history, we deplore the idea that those in the public eye should suffer professionally for having the courage to give voice to their views. Even a hint of the blacklist must never again be tolerated in this nation!" </P>
<P>Bravo SAG and IATSE!</P>
<B><P>Tim Cook,</B> Los Angeles</P>

