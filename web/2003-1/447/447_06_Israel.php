<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Is this a war for Israeli interests?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nihar Bhatt</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 4, 2003 | Pages 6</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IS THE U.S. war on Iraq driven by Washington's support for Israel? Does Israel have more to gain from this war than the U.S.? Some opponents of the war--from both right and left--have answered "yes" to these questions.</P>
<P>Obviously, there's a connection between the two. Israel is a zealous supporter of the U.S. and British invasion. Israeli Prime Minister Ariel Sharon is using it as cover to escalate his offensive against Palestinians in the West Bank and Gaza. And Sharon and other Israeli right wingers hope this war will accomplish something that they have long urged--a sharply increased U.S. military presence in the Middle East.</P>
<P>But is protecting Israel the key motivation for a U.S. war that otherwise isn't in America's interests? There are several versions of this argument. Rep. Jim Moran's (D-Va.) comment at a March 3 church forum in Virginia--"If it were not for the strong support of the Jewish community for this war with Iraq, we would not be doing this"--borders on anti-Semitism.</P>
<P>The idea that "Jews" as a unified entity have power over the actions of the Pentagon is absurd--and perpetuates an anti-Semitic conspiracy theory. Jews, like all Americans, are divided over the war. In fact, opinion polls show that a much higher percent of white evangelical Christians support the war than Jews.</P>
<P>The more common version of the argument in antiwar circles is that there is a convergence of interests between the "Israel lobby" and the hard-line hawks, like Richard Perle and Paul Wolfowitz, who are leading the Bush administration's war drive.</P>
<P>According to this argument, Iraq obviously poses no real threat to the U.S. The only country with something to gain from a war, therefore, must be the only country in the region to claim that Iraq is a threat: Israel.</P>
<P>But this only makes sense if you forget what the U.S. government has to gain from war on Iraq--the opportunity both to control the world's second-largest oil reserves and to remake the world order to serve the interests of U.S. empire.</P>
<P>It certainly is true that hawks like Perle and Wolfowitz are committed to defending Israel and its war crimes against Palestinians--and that they have lead the charge for a pre-emptive war. But as Stephen Zunes writes, "These same neo-conservatives, while in the Reagan administration during the '80s, were advocates of a U.S. invasion of Nicaragua and Cuba, as well as a nuclear first strike in a so-called limited nuclear war with the Soviet Union. In short, they are hawks across the board, not just in regard to the Middle East." </P>
<P>Pro-Israel lobby groups such as American Israel Public Affairs Committee have not launched significant campaigns around war with Iraq. Even if they had, the actual influence that these groups have in Washington pales in comparison to the big oil companies and military contractors. </P>
<P>A far more powerful interest than pro-Israel organizations lies behind both U.S. support for Israel and the war on Iraq. It is the interest of U.S. imperialism in dominating the Middle East and controlling its oil. </P>
<P>Israel's massive military dominance in the region has allowed it to do some of the "dirty work" of policing the Middle East for the U.S.--such as containing Arab nationalist movements in Lebanon, Jordan and Palestine and keeping other regional powers like Iran and Syria in check. But that's not good enough for the hawks of the Bush administration--which is why they want a new war on Iraq to deepen their hold. </P>
<P>There is a connection between the war on Iraq and U.S. support for Israel. Both are part of the broader drive for U.S. empire in the Middle East. </P>


