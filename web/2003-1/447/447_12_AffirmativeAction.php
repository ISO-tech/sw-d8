<P><font face="Times New Roman, Times, serif" size="4">New challenge to affirmative action</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Bush backs racist attack</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 4, 2003 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE FUTURE of affirmative action hung in the balance as the U.S. Supreme Court was set to hear arguments April 1 in a lawsuit challenging admission policies at the University of Michigan. Not since the infamous Bakke case in 1978--in which affirmative action was upheld, but "racial quotas" were banned--has such a decisive attack on affirmative action been launched.</P>
<P>The anti-affirmative action lawsuits were filed on behalf of white applicants to the University of Michigan's undergraduate program and its law school. These applicants claim that they were skipped over in favor of less qualified Black and Latino candidates. Like Allan Bakke, they say that the Michigan policy is "reverse discrimination" and that it unfairly gives "preferential treatment" to minority applicants.</P>
<P>George W. Bush lent his name to the attack when he announced that the White House would file a so-called "friend-of-the-court" brief opposing Michigan's admissions policies. The right-wing groups leading this assault--the misnamed Center for Equal Opportunity and American Civil Rights Institute--may have the president on their side. But not many others.</P>
<P>Hundreds of individuals and organizations--including military and corporate leaders--have signed onto 78 briefs in favor of Michigan's policy. Activists have organized a protest for April 1 in support of affirmative action at the Supreme Court building in Washington.</P>
<P>The decision in this case will be crucial. The Center for Equal Opportunity and American Civil Rights Institute have already set their sights on at least 30 other universities--including Iowa State, Princeton and the Massachusetts Institute of Technology.</P>
<P>These racist attacks have nothing to do with civil rights. Don't let them turn back the clock on affirmative action!</P>


