<P><font face="Times New Roman, Times, serif" size="5"><b>Feds take aim at foreign students</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 4, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE FEDERAL government's witch-hunters have found a new group to attack: Some 580,000 foreign students studying in the U.S.</P>
<P>In late January, the Student and Exchange Visitor Information System (SEVIS)--a mandatory program linking all colleges and universities that enroll international students to the Department of Homeland Security--was rolled out.</P>
<P>SEVIS tracks every move that foreign students make--from traveling outside the U.S., to their employment, to the number of courses they take--and passes the information along to the Bureau for Citizenship and Immigration Services (formerly the Immigration and Naturalization Service). Anyone without their records in order can be deported.</P>
<P>There's just one problem: the $36 million system is full of glitches and malfunctions. On March 12, for example, a student from Thailand studying at Southeastern University in Washington, D.C., had federal agents show up at her door because her SEVIS record wrongly showed that she had dropped out of school. Because the agents discovered that the young woman worked part time, possibly in violation of her visa, they led her away in handcuffs.</P>
<P>The new system is so unreliable that at Indiana University, the Office of International Services informed international students and faculty that they have to carry their passports and visas with them at all times--and are liable to be deported if questioned by an officer without the documents.</P>
<P>Foreign students have been caught up in the witch-hunt ever since September 11--particularly young Muslim and Arab men from a number of countries, who have been forced to undergo immigration "registrations."</P>
<P>University of Colorado student Yashar Zendehdel, for example, was one of six Colorado students arrested and threatened with deportation in late January--because they had failed to take a full 12 hours of college credit per semester, an INS requirement.</P>
<P>In Zendehdel's case, college advisers recommended that he drop a class for academic reasons. "I really get mad thinking about what happened to me," Zendehdel told the <I>Rocky Mountain Collegian. </I>"I came here because of the higher standards of higher education, but if they want to continue like this, I'm not sure I want to go on."</P>
<P>These attacks are just one more attempt by the Bush administration to intimidate immigrants and non-citizens. But as Charlene Drew Jarvis, president of Southeastern University, told the <I>Chicago Tribune</I>: "You can't fight terrorism by terrorizing the students."</P>


