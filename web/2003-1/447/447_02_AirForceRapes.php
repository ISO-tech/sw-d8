<P><font face="Times New Roman, Times, serif" size="4">Women Air Force cadets sexually assaulted</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>An academy for rapists</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 4, 2003 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH says that his war on Iraq will put an end to human rights atrocities carried out in "torture chambers" and "rape rooms." Meanwhile, more than 50 women have come forward to describe how their complaints of rape and sexual assault at the U.S. Air Force Academy in Boulder, Colo., were shrugged off and covered up by higher officers.</P>
<P>Last week, the Air Force responded by dismissing four top officers at the Academy: Gen. John Dallager, Brig. Gen. S. Taco Gilbert III, Col. Robert Eskridge and Col. Laurie Slavec. </P>
<P>One woman who was raped after a party says that when she reported the attack to Gilbert, he told her: "You didn't have to go to that party. You didn't have to drink that night." Another woman told the <I>Denver Post</I> that Gilbert suggested she run 50 laps alongside her alleged assailant for punishment--because she had been drinking when the attack happened.</P>
<P>Kira Mountjoy-Pepka, says that when she reported being raped by DonCosta Seawell, a senior at the Academy, some officers told her she was "no lady" and suggested that her behavior was promiscuous. Seawell, meanwhile, had been accused of sexually assaulting a civilian in California three months earlier, as well as another freshman cadet at the Academy more than a year before that. </P>
<P>Air Force officials claim that they were "unaware" of the scope of the "rape rooms" at the Academy. But in 1997, an annual survey of cadets showed that 10 percent of women responding said they had been the victim of a sexual assault at the Academy in the previous 12 months. Some 75 percent said that if they were raped, they would not report it out of fear of retribution.</P>
<P>"It's not a problem of a few bad cadets," Mountjoy-Pepka told the <I>Washington Post</I>. "It's a problem of a few bad generals."</P>


