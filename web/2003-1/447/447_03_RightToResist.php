<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Why the Iraqi opposition to U.S. invasion is justified</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The right to resist</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 4, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WHEN it sent U.S. troops into South Vietnam in 1965, Washington vowed to free the country from the threat of Ho Chi Minh, the supposed "dictator" of North Vietnam.</P>
<P>The vast majority of South Vietnamese welcomed American soldiers as liberators, U.S. officials claimed. The only armed resistance, they said, came from small groups of guerrillas from the "Viet Cong"--the National Liberation Front--who hid among civilians and terrorized ordinary people into opposing the U.S.</P>
<P>Washington kept peddling this same lie until April 1975--when the North Vietnamese Army swept into the South to an overwhelmingly popular welcome. Today, the same lie is being repeated in Washington--this time, about resistance to the U.S. invasion of Iraq.</P>
<P>The war planners are getting hammered because their arrogant predictions of instant victory were proved completely wrong. So they've resorted to blaming the "brutal thugs" of Saddam Hussein's fedayeen militia for preventing the Iraqi people from welcoming U.S. troops with cheers and flowers. "[T]he strategy in Baghdad is to use guerrillas--Baath Party Viet Cong--to harass our troops everywhere, in order to demoralize America and achieve a negotiated peace," <I>New York Times</I> columnist William Safire wrote.</P>
<P>But even the pro-war media has had to acknowledge that most Iraqis see U.S. troops not as liberators, but as invaders--out to re-colonize the country to exploit its oil wealth. MSNBC reporter Mike Taibbi described a conversation with a woman--one of more than 4,000 Iraqis returning from Jordan--who said that if she couldn't get a gun to fight the Americans, she would use her hands and teeth.</P>
<P>In the town of Nasiriya, where Marines were waging a bloody week-old battle for control of the streets as <I>Socialist Worker</I> went to press, "almost any man between boyhood and old age is a 'military-age male,' subject to detention" by U.S. forces, the <I>New York Times</I> reported. "No Iraqi will support what the Americans are doing here," one Iraqi man told a reporter. "If they want to go to Baghdad, that's one thing, but now they have come into our cities, and all Iraqis will fight them."</P>
<P>Journalist Seymour Hersh of the <I>New Yorker</I> quoted a former CIA agent, Robert Baer, on the scale of the popular resistance in Iraq. "Everybody wants to fight," Baer said from Jordan. "The whole nation of Iraq is fighting to defend Iraq. Not Saddam. They've been given the high sign, and we are courting disaster."</P>
<P>Iraqis are right to resist the U.S. invasion--and they deserve the support of the antiwar movement in the U.S. Those fighting are not the puppets of a "dictator." They are motivated by their hatred of a U.S. invasion and occupation that comes after 12 long years of military and economic warfare that has already laid waste to Iraq.</P>
<P>The words of Martin Luther King Jr., in his 1967 speech opposing the Vietnam War, ring true about the war in Iraq today. "They must see Americans as strange liberators," King said. "They watch as we poison their water, as we kill a million acres of their crops. They must weep as the bulldozers roar through their areas, preparing to destroy the precious trees. They wander into the hospitals, with at least 20 casualties from American firepower for one 'Viet Cong'-inflicted injury&#133;.What do they think as we test our latest weapons on them, just as the Germans tested out new medicine and new tortures in the concentration camps of Europe?"</P>
<P>The U.S. government doesn't want to liberate Iraq, but to conquer it--and subjugate the Iraqi people in the interests of oil profits and imperial power. The popular resistance against the invasion of Iraq is justified--and an inspiration for opposition to U.S. imperialism around the world.</P>


