<P><font face="Times New Roman, Times, serif" size="4">Bush's $75 billion war budget</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Ripping us off to pay for war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 4, 2003 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH wants guns alright. But instead of "guns and butter," he wants "guns and caviar."</P>
<P>Last week, the Bush administration proposed its emergency budget to pay for the U.S. war on Iraq--$75 billion added to a Pentagon budget that was already larger than spending on the next 20 biggest militaries combined.</P>
<P>At the same time, the White House tried to shove its new budget through Congress--a plan that includes some $726 billion in tax cuts over the next decade, most of which will go to the very richest Americans.</P>
<P>But even a few fellow Republicans think that massive new military spending and huge tax breaks--coming while the economy is still staggering--are a bit much. In a stunning blow to a wartime president, the Republican-controlled Senate cut the Bush tax cut proposal in half.</P>
<P>Three GOP senators crossed over to join Democrats in trimming the White House giveaway. It was a welcome sight to see the Bush administration taken down a peg--even if the media barely noticed because of the war.</P>
<P>But the Senate "compromise"--$350 billion in tax cuts--is still much too high, especially coming on the heels of Bush's $1.3 trillion giveaway in 2001. And this budget fight isn't over. The Senate and the House will wrestle over the final details, and their agreement will likely favor the Bush plan.</P>
<P>It's clear that U.S. workers and the poor will be ripped off to pay for Bush's war and to make the rich even richer. The only question is how much. Make no mistake: The politicians of both parties plan to pay for all this by hacking away at what's left of the battered social safety net.</P>
<P>Of the $265 billion in proposed spending cuts to what Republicans call "waste, fraud and abuse," three-fifths of the reductions will fall on the poor, disabled or the very young. According to the Center on Budget and Policy Priorities, if the Republican budget cuts go through:</P>
<P>--The average food stamp benefit will be cut from an already paltry 91 cents a meal to 84 cents.</P>
<P>--Some 476,000 low-income elderly disabled people could be dropped from Supplemental Security Income.</P>
<P>--More than 13.5 million children could lose Medicaid health coverage.</P>
<P>--Almost 2.5 million low-income children could lose free school lunches.</P>
<P>All this while the need for these programs only grows. Recent reports show that after years of decline, the number of people who requested food stamps jumped by 22 percent over the last two years. And with individual states facing the worst budget crisis since the Great Depression, cuts at the state level are already the order of the day.</P>
<P>The amount of money that Bush wants for his Iraq war could pay for more than 1 million elementary school teachers--or provide health care for 26 million children. And the $350 billion that the Senate wants to allow Bush to hand out in tax cuts could cancel out all of the proposed cuts in social services, with billions to spare.</P>
<P>These are the sick priorities of the Bush administration. We have to expose this rip-off--and organize the fight against the politicians' war on the poor.</P>

