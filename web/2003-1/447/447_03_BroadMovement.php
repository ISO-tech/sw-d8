<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Building a broader movement</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 4, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>AS SOON as the war on Iraq began, huge protests erupted around the U.S. From the emergency demonstrations on the first night, to the pickets and protests in the days since, the outpouring of opposition was too big even for the gung-ho corporate media to ignore. To be a part of this movement with so many other people has been exhilarating. But the feelings of antiwar activists are bound to be mixed. </P>
<P>Our protests have been unprecedented in their size and scale--and still the maniacs in Washington got their war. This has fed a sense among some activists that demonstrations aren't enough now--that we need to organize more dramatic actions, centered around civil disobedience and direct action, to really show our opposition.</P>
<P>This sentiment is understandable. But the actions of a minority of activists, no matter how determined, can't substitute for building a bigger and broader movement.</P>
<P>It's wrong to conclude that antiwar demonstrations haven't had an impact. They've affected the thinking of huge numbers of people--not only those who have taken part, but many others who saw in them expressions of their own doubts, even if they haven't taken action themselves.</P>
<P>Because of their worldwide scale, there is a greater sense of internationalist solidarity among those who oppose this war. And while no one in Washington will admit it, the size and scale of protests around the world are an important reason for some of the problems that the U.S. is facing in its war on Iraq.</P>
<P>These demonstrations are building blocks for a struggle that has to be organized for the long term--to oppose not only the war on Iraq for as long as it lasts, but the military occupation to follow, and to defend the other targets of U.S. aggression to come.</P>
<P>What's more, the belief that activists simply need to be "more militant" will take the movement in the wrong direction--with the most committed activists isolating themselves from a wider audience. The vast majority of people who are newly involved in antiwar activity don't share the assumptions of the small groups of activists who have concluded that "demonstrations aren't enough." This wider group doesn't want to be arrested at their first demonstration. And they aren't "tired of demonstrating."</P>
<P>During the struggle against the Vietnam War, some dedicated activists concluded that the political establishment wasn't responding to mass protests--and that anyone who was truly committed should be willing to put themselves on the line.</P>
<P>The most extreme example was the Weather Underground--an illegal armed organization formed by leaders of the massive Students for a Democratic Society on the belief that this was needed to push the fight forward. Just the opposite was the case--Weather members went to jail, and the group's actions alienated potential opponents of the war, rather than drawing them into struggle.</P>
<P>The Weather Underground didn't stop wider masses of people from turning against the war. But it did mean that experienced leaders of the 1960s movement removed themselves from the struggle, just as more and more people were becoming radicalized.</P>
<P>The new antiwar movement has made great strides. But it has to become larger and broader still. That requires organizing activities to embrace those who are questioning the war, rather than cutting ourselves off from them. "Old-fashioned" activities like demonstrations take on new energy and grow larger when they bring together people who have recently gained the confidence to take a stand.</P>
<P>And we have to insist that civil disobedience and direct action confined to small groups of activists don't prove a greater commitment to the cause than the patient work of building a movement that can draw in larger numbers of people for the long haul.</P>
<P>We need to keep organizing the widest possible expressions of antiwar opposition--and make sure that the movement speaks not only for the already committed, but for everyone who wants to say no to Bush's war.</P>


