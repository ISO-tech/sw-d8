<P><font face="Times New Roman, Times, serif" size="5"><b>Antiwar protests around the world</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 4, 2003 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>FOR THE second straight week, protests against the U.S. war on Iraq erupted around the world. In Germany, 50,000 marched through the streets of Berlin to denounce the war. "Send inspectors to check for weapons of mass destruction in America," read one of the many handmade signs on the demonstration.</P>
<P>According to antiwar organizers, there were 67 demonstrations, pickets and protests in Berlin in the first 10 days after the war began. And 30,000 demonstrators formed a 30-mile human chain between the cities of Osnabruck and Munster, a famous route traveled by negotiators who ended the devastating Thirty Years War in 1648. About 6,000 protesters formed a blockade around a major U.S. military installation in Stuttgart before police dispersed them.</P>
<P>In Spain, 25,000 people marched on the Rota Naval and Air Base where U.S. forces are stationed. In Athens, Greece, 15,000 marched on the U.S. embassy chanting "We'll stop the war." Protesters splashed red paint on the road outside the building and on the windows of a nearby McDonald's.</P>
<P>In S&atilde;o Paulo, Brazil, 5,000 took to the streets against the war. "Who does Bush think he's kidding when he says that the war will free the Iraqi people of a tyrant?" asked Fernanda Alvarez, a marketing director for a S&atilde;o Paolo cosmetics company. "Bush's war is just a vehicle to expand U.S. power, and if this goes, war will never stop because U.S. greed will never be satiated."</P>
<P>In Rabat, Morocco, 200,000 antiwar demonstrators marched through the city--throwing stones and tearing down posters advertising American goods. In Indonesia, roughly 200,000 marched on the U.S. embassy in Jakarta. In Pakistan, the fourth in a series of marches organized by Islamic forces drew 100,000 in the city of Peshawar.</P>
<P>In South Korea, 30,000 workers demanded that the National Assembly vote down a bill that calls for sending 700 military engineers and medics to support the U.S. war. And students in Seoul clashed with riot police as thousands filled a boulevard chanting "Stop the bombing! Stop the killing!"</P>
<P>In the Middle East, 100,000 Palestinians commemorating Land Day--the day in 1976 when Israeli security forces killed six Palestinians protesting the Israeli seizure of Palestinian land--turned a demonstration in the city of Sakhnin into an antiwar protest.</P>
<P>About 15,000 students in Alexandria, Egypt, burned U.S. and British flags and called for action to help the Iraqi resistance. Even in Iran, which fought a devastating 10-year war with Iraq, tens of thousands of protesters took to the streets of Tehran to denounce the U.S. war.</P>


