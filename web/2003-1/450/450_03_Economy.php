<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The economy that war didn't fix</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 25, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>A QUICK end to the war on Iraq was supposed to boost the U.S. economy by lowering oil prices and removing a source of international uncertainty. Oil prices did fall--by a whopping 30 percent in a month. And Wall Street investors began searching for bargains, pushing the stock market up 10 percent from its mid-March lows.</P>
<P>But these surface developments mask underlying weaknesses so glaring that even mainstream economists are taking note. "Postwar euphoria is fading, revealing an economy still mired in structural problems such as overcapacity, high consumer debt and weak corporate profits," wrote <I>Business Week</I> in late April.</P>
<P>The International Monetary Fund's <I>World Economic Outlook</I> released in early April noted that this weakness preceded the war on Iraq. "The U.S. economy failed to sustain in the second half of last year the momentum it assumed as it emerged from recession at end-2001, with growth in the fourth quarter of 2002 turning very low--the 'soft spot,'" reads the report.</P>
<P>But for millions of people looking for a job, the economy is worse than soft. "The last two monthly job reports for February and March show a combined loss of almost half a million jobs," wrote Robert Reich, labor secretary under Bill Clinton. "So far, this recession has spawned the longest continuous decline in jobs in half a century."</P>
<P>The economy is now the top concern among voters, according to a poll conducted by the Winston Group, a Republican polling firm. Fully 29 percent of voters name jobs as the most important issue in the 2004 elections. Only 16 percent cite national security or terrorism.</P>
<P>George W. Bush's only answer to the economic slump is to demand even more massive tax cuts whose benefits will go overwhelmingly to the already filthy rich.</P>
<P>The Democrats ought to be able to stick it to Bush. Instead, they're patting themselves on the back for reducing the size of Bush's tax cut in a Senate vote that cut the proposal back from $726 billion to $350 billion. The so-called "party of the people" won't challenge the basic logic of the tax cut--which takes money from government programs that benefit workers and the poor and gives it to the rich.</P>
<P>Instead of passing tax cuts for the rich, Congress should extend unemployment benefits and increase spending on public assistance. We've had enough of Bush's war on workers.</P>


