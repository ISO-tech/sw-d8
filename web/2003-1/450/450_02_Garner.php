<P><font face="Times New Roman, Times, serif" size="4">Garner says Bush would have won Vietnam</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Iraq's colonial overseer</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 25, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IRAQ'S DEFEAT has filled Jay Garner's head with visions of limitless U.S. military power. Garner is the retired three-star general who will be Iraq's new colonial overseer. Last week, drunk with news that the U.S. had overwhelmed an already decimated Iraq, Garner gushed to the <I>New York Times</I> that "if President Bush had been president, we would have won" the Vietnam War. "We should have taken the war to the north instead of waiting in the south," Garner declared.</P>
<P>The U.S. did, in fact, carpet-bomb North Vietnam. But it didn't invade the north. "A U.S. invasion of North Vietnam would have engaged the risk of superpower nuclear conflict" by drawing in the former USSR and China, according to an article in the <I>New Republic</I> lambasting Garner's historical revisions.</P>
<P>Instead, the U.S. opted for "insulating" peasant villages in South Vietnam from the influence of the North Vietnamese fighters with a "strategic hamlet" program--a disastrous scheme that Garner himself served as a senior adviser to.</P>
<P>The program involved the relocation of whole villages to areas controlled by U.S. forces. "Tens of thousands of peasants were forced to leave their homes and build new ones surrounded by barbed wire barricades," the <I>New York Times</I> reported. "Communist propaganda focused effectively on the most objectionable aspects of the program, calling the hamlets concentration camps...This charge, according to U.S. officials, was all too accurate in many instances."</P>
<P>Garner served two tours in Vietnam and remained in the military until 1997--when he got a job at defense contractor SYColeman, a firm that has already collected $1 billion in Pentagon contracts this year. And this is the man who the Bush administration wants to run Iraq's new democracy?</P>


