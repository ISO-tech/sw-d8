<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Before Saddam fell from favor</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 25, 2003 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE U.S. may be indifferent to the sacking of thousands of years of Iraqi history in museums and libraries, but they're actively trying to erase some more recent history--the history of U.S. collaboration with Saddam Hussein.</P>
<P>The CIA recruited Saddam to organize a 1959 assassination of radical nationalist Iraqi president Abdel Karim Qasim. Saddam botched the job. So the CIA helped him escape to Syria, and then paid to support him in Egypt for the following four years, according to United Press International (UPI) reporter Richard Sale.</P>
<P>The CIA chose Saddam's authoritarian, anticommunist Baath Party "as its instrument" in Iraq, former National Security Agency staffer Roger Morris told Sale. Sale's informants disputed whether the CIA--and President John F. Kennedy--was behind the 1963 Baathist coup that overthrew Qasim. But they all agreed the CIA took advantage of the situation to eliminate challengers to Baathist rule.</P>
<P>The CIA supplied Baathist national guardsmen with lists of communists and activists. The Baathists used the list to torture and execute thousands. Saddam presided over the mass executions. "We were frankly glad to be rid of them," a former U.S. State Department official told the UPI reporter. "You ask that they get a fair trial? You have to [be] kidding. This was serious business."</P>
<P>Despite all the huffing and puffing about Saddam's cruelty emanating from Washington, the history shows that the U.S. built up Saddam and the Baathist dictatorship it now claims to oppose.</P>
<P>Iraqi-U.S. relations had a renaissance in the early 1980s after the U.S. lost its preferred Persian Gulf strongman, the Shah of Iran, in the 1979 Iranian revolution. Recently declassified documents posted on the National Security Archive's Web site tell the story of U.S. collaboration with Saddam during the Iran-Iraq war in the 1980s.</P>
<P>Only a few months after Reagan took power in 1981, the U.S. interests section in Baghdad wrote the State Department to urge increased ties with Iraq because there was "a greater convergence of interests with Iraq than at any time since the revolution of 1958."</P>
<P>As it increased military, intelligence and economic aid to Iraq, the U.S. knew that Iraq was using chemical weapons against Iranian and Kurdish forces. "We also know that Iraq has acquired a CW [chemical weapons] production capability, presumably from Western firms, including possibly a U.S. foreign subsidiary," a memo to Secretary of State George Shultz said.</P>
<P>The U.S. strategy was to criticize Iraq's use of chemical weapons, but to more strongly condemn Iran's "intransigent refusal to deviate from its avowed objective of eliminating the legitimate government of neighboring Iraq," a 1984 "talking points" memo circulated in the State Department read.</P>
<P>In 1984, the U.S. helped to derail a Security Council resolution condemning Iraq's use of chemical weapons. Commenting on behind-the-scenes maneuvering to water down the resolution, a State Department memo assured that the accepted text "contains all three elements [Iraq's UN ambassador] wanted."</P>
<P>Another 1984 State Department memo recommends the U.S. allow trade with "Iraqi nuclear entities" known to be developing nuclear weapons.</P>
<P>President Reagan dispatched Donald Rumsfeld on two high-level missions to Iraq in 1983 and 1984. A State Department cable summarizing Rumsfeld's 1983 meeting speaks of Saddam's "obvious pleasure" with a letter from Reagan that Rumsfeld delivered.</P>
<P>Of course, all of these good feelings came to an end when Iraq invaded Kuwait in 1990. Overnight, Saddam's regime went from being allies to Hitlerian fascists. But Saddam hadn't changed. Washington's assessment of his reliability had. And so he had to be removed.</P>
<P>From Washington's point of view, the end of Saddam's reign isn't a signal for Iraqis to control their own lives. It's a signal that the exploitation of their oil--and the repression that facilitates it--is under new management.</P>


