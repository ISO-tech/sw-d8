<P><font face="Times New Roman, Times, serif" size="5"><b>Say no to Bush's war on Iraq</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 25, 2003 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>AS THE U.S.'s brutal war on Iraq moves on to a brutal occupation, antiwar activists around the country are coming together to discuss strategies for maintaining a broad opposition.</P>
<P>The Campus Antiwar Network (CAN)--formed by antiwar activists at high schools and colleges around the country in February--is organizing regional conferences to discuss plans for upcoming activities.</P>
<P>"It's important at this time that we come together to talk about ways that antiwar activists can relate to the many questions and doubts that still exist out there about the U.S. war on Iraq," said Leticia Arellano of San Francisco State University Students Against War. "The U.S. government is claiming victory, and that it 'liberated' the Iraqi people. We have to find ways to continue exposing the real U.S. aims."</P>
<P>On April 5, CAN helped mobilize thousands to protests in the Bay Area and Chicago. At the upcoming conferences--which will take place in California, Chicago, Rhode Island and Texas--activists have planned discussions of important questions, such as how to address the U.S. occupation of Iraq and George W. Bush's never-ending "war on terrorism."</P>
<P>Students will also discuss activities over the summer, including educational materials to further the antiwar movement and divestment campaigns for the fall.</P>
