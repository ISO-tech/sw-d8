<P><font face="Times New Roman, Times, serif" size="5"><b>What tactics will help our movement grow?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 25, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear Socialist Worker,</P>
</B><P>Anonymous Aaron's letter ("Where is <I>Socialist Worker</I>'s solidarity?" April 11) is way off the mark in attacking <I>SW</I>'s editorial on civil disobedience. Aaron argues, "Political activists--including you--conduct actions every day in an isolated fashion." But there is a difference between conducting actions in an isolated fashion and conducting actions in an isolating fashion.</P>
<P>On principle, we oppose the war on Iraq, and we opposed the war on Afghanistan. There were not millions-strong protests during the Afghan war. Socialists and other activists were, in a sense, "isolated" in their opposition. All of our actions, though, no matter how small, were aimed at winning a larger segment of the population to support our views.</P>
<P>This is the basis for judging what tactics we should use. There are times when civil disobedience is very appropriate--most of all, when large numbers of people can participate, rather than a select few.</P>
<P>But it was also important to recognize that many people coming to the antiwar demonstrations in the weeks before and after the war were at their first political activities. They were against the war, but not, by and large, ready to get arrested. </P>
<P>Does that mean we should reject their participation? We don't want people new to activity--and those who saw reports of the protests and were thinking about taking a stand themselves--to come away believing that you have to be ready to spend the night in jail to show your opposition to Bush's war.</P>
<P>At this stage, a dedicated minority of antiwar activists may be prepared to take more "militant" action. But we can't organize our strategy for building our movement around this minority, while ignoring those who we want to involve.</P>
<B><P>John Green and Elizabeth Schulte</P></B>

