<P><font face="Times New Roman, Times, serif" size="4">A new collection of essays by Arundhati Roy</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Laying siege to empire</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Chris Fagan</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 25, 2003 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>BOOKS: <I>War Talk,</I> </B>Arundhati Roy, South End Press, 2003, 142 pages, $12.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>THESE DAYS the political spectrum in the corporate media seems to range from completely mad to raving mad. A new book from writer and activist Arundhati Roy would be welcome at any time, but it is particularly so now. </P>
<I><P>War Talk</I> is a short collection of previously published essays, written over the past year. Ranging from a poignant piece about living in the shadow of nuclear war, to the speech she gave at the World Social Forum this year, aptly titled "Confronting Empire," the collection is a great antidote to the blathering of most pundits and "intellectuals."</P>
<P>Roy lives in Delhi, India. During the nuclear stand-off between India and Pakistan, in response to the question "Why don't you leave?" she writes, "But where shall we go? Is it possible to go out and buy another life because this one's not panning out? If I go away, and everything and everyone&#133;that I have known and loved--is incinerated, how shall I live on? Whom shall I love? And who will love me back?"</P>
<P>There are few sharper critics of globalization and imperialism than Roy. Readers of her previous political writing will recognize in <I>War Talk </I>her uncompromising opposition to unbridled power and the demise of real democracy. But what sets her writing apart is her articulation of the human cost of the madness of the world, and fierce denunciation of the arrogance of power.</P>
<P>In an essay written after the fascist pogrom against Muslims in Gujurat that left 2,000 people dead, she says: "While Gujurat burned, our Prime Minister was on MTV promoting his new poems&#133;It took him more than a month--and two vacations in the hills--to make it to Gujurat. When he did, shadowed by the chilling Modi, he gave a speech at the Shah Alam refugee camp. His mouth moved, he tried to express concern, but no real sound emerged except the mocking of the wind whistling through a burned, bloodied, broken world."</P>
<P>The book is permeated by both Roy's eloquent outrage at the crimes of the powerful, and a belief in the capacity of ordinary people to create a different kind of future. The speech "Confronting Empire" ends with this: "Our strategy should be not only to confront Empire, but to lay siege to it. To deprive it of oxygen. To shame it. To mock it. With our art, our music, our literature, our stubbornness, our joy, our brilliance, our sheer relentlessness--and our ability to tell our own stories&#133;Remember this: We be many and they be few. They need us more than we need them."</P>
<P>Those looking for dry analysis of the world should look elsewhere. But for anyone looking for a voice of sanity and compassion against the Bushes of the world, reading <I>War Talk </I>is a great way to spend a couple of hours.</P>


