<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The Bush agenda in the Middle East</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 25, 2003 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>EVEN AS gunshots and tank engines echoed through the streets of Baghdad, U.S. officials began threatening another Middle Eastern country with military annihilation. "Syria has been a concern for a long period of time," Secretary of State Colin Powell told the BBC last week. "We are making this point clearly and in a very direct manner to the Syrians."</P>
<P>It may seem risky for the U.S. to begin preparations for another war--especially considering the broad international opposition to the war on Iraq and the growing opposition among Iraqis to the U.S. occupation. But for one wing of the U.S. establishment, a war that causes upheaval and volatility is part of an overall strategy.</P>
<P>"Whenever I hear policymakers talk about the wonders of 'stability,' I get the heebie-jeebies," wrote Michael Ledeen, one of the many "hawks" at the American Enterprise Institute. "That is for tired old Europeans and nervous Asians, not for us&#133;We are not going to fight foreign wars or send our money overseas merely to defend the status quo; we must have a suitably glorious objective."</P>
<P>The idea that the U.S. represents the pinnacle of human civilization and should fulfill its destiny by remaking other countries in its image is not a new idea. "Warlike intervention by civilized powers would contribute directly to the peace of the world," said President Theodore Roosevelt 100 years ago. "All the great masterful races have been fighting races," continued Roosevelt, adding the theme of white supremacy that today's neo-conservatives like Ledeen are careful not to say--at least not in public.</P>
<P>But what is new about the neo-conservative foreign policy agenda promoted by Bush administration officials like Deputy Defense Secretary Paul Wolfowitz and Undersecretary of State John Bolton is its open disregard for existing international relations. Laws, judges and trials may be suitable "for our domestic political process," according to Wolfowitz. But "foreign policy decisions cannot be subject to that kind of rule of law."</P>
<P>As the world's lone superpower, Wolfowitz and his co-thinkers believe, the U.S. should use pre-emptive military action to curb all challenges, even in the face of objections from traditional allies or international bodies like the United Nations.</P>
<P>In the words of the Project for a New American Century, a neo-conservative think tank with significant influence in the Bush administration, the aim should be "maintaining U.S. pre-eminence, precluding the rise of a great power rival and shaping the international security order in line with American principles and interests."</P>
<P>In this scenario, wars in the name of "spreading democracy" can serve a dual purpose. They may succeed in replacing Middle Eastern governments with regimes "friendly" to U.S. interests. Or, if they fail to bring about "regime change," the resulting instability will lead to the fragmentation and disintegration of various Arab nations--an outcome that's also welcome in the eyes of the hawks.</P>
<P>"It's a war to turn the kaleidoscope, by people who know nothing about the Middle East," said Chas. Freeman, former U.S. ambassador to Saudi Arabia. "And there's no way to know how the pieces will fall."</P>
<P>Pre-emptive war is only one of the tactics in the hawks' repertoire. In the case of Syria, for example, the Bush administration is threatening military action at the same time as it escalates diplomatic and economic pressure. Thus, for example, the U.S. military rulers of Iraq last week closed an oil pipeline to Syria, dealing a $1 billion blow to Syria's economy. And Congress is considering passage of the Syria Accountability Act, which would impose sanctions on U.S. investment in Syria and restrict the movement of Syrian diplomats in the U.S.</P>
<P>The hope is to cash in on the demonstration of savage American power on display in Iraq. But whether they use war or other means, their goal remains imperialist domination over oil and maintaining political superiority--in the Middle East, but also against competitors in Europe and Asia.</P>


