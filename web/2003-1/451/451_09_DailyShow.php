<P><font face="Times New Roman, Times, serif" size="4"><I>Daily Show</I> skewers the media hypocrites</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>TV's best news show?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Alan Maass</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 2, 2003 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>TELEVISION: <I>The Daily Show with Jon Stewart,</B></I> Monday through Thursdays at 11 p.m. EDT on Comedy Central.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>QUICK--WHAT'S the best source of news on television?</P>
<P>Peter Jennings or Dan Rather? Nice hair, but no way. CNN? Fox News? Increasingly indistinguishable fountains of Pentagon propaganda. PBS? Slavishly devoted to a narrow political spectrum of Washington establishment figures. And a snoozer to boot.</P>
<P>No, clearly, the best source of news on television is <I>The Daily Show </I>on the Comedy Central cable network. Okay, so it is a <I>fake </I>news show, a parody produced four nights a week by a team of comics headed by Jon Stewart.</P>
<P>But as Michael Moore said in his Oscar speech, we live in "fictional times." Every reader of <I>Socialist Worker </I>must have, at some point in the past few months, shaken their heads at the "real" television news--stunned by the distortions, ignorance and outright lies. Is it really so surprising that there's more truth on a satire of the nightly news, than on the nightly news itself?</P>
<P>A lot of <I>The Daily Show's </I>humor comes from its send-up of television journalism, with its clich&eacute;-spouting reporters and self-important "senior analysts." But the show also gets laughs from the news itself, cutting through the propaganda and skewering the establishment view on the war, the economy, Washington politics and more.</P>
<P>So, for example, when Donald Rumsfeld began threatening Syria and Iran during his Pentagon war briefings, Stewart spoke for millions when he said: "It really makes you wonder what the fuck he's thinking...We're in the middle of a war, and he's starting another war,. We're already fighting Iraq, and he's like, 'By the way, Syria, you want a piece?'...There's nothing like a cantankerous old man who takes a hey-you-kids-get-off-my-lawn approach to foreign policy."</P>
<P>Stewart insists that he isn't a radical, and he does adopt a respectful attitude to even right-wing hacks in the interviews that end each episode. But in trying to expose the hypocrisy and propaganda of the regular news, <I>The Daily Show </I>inevitably ends up at odds with the powers that be--especially, as <I>Salon's </I>Laura Miller pointed out, with "plutocratic religious fanatics with imperialist ambitions occupying the White House."</P>
<P>This goes against the dominant trend--seen on Comedy Central in great heaping piles on either side of <I>The Daily Show's </I>nightly slot--of what could be called "comedy of playground bullies."</P>
<P>The likes of Dennis Miller or the morons on <I>The Man Show </I>get their laughs by picking on those below them and flattering those above. They appeal to existing prejudices and rely on the stereotypes drummed into people--from racist or sexist caricatures to more commonplace assumptions that human beings are hopelessly stupid and crude. In addition to being obnoxious, this kind of comedy is profoundly conservative, because it reinforces all of the "common sense" ideas that prop up the status quo.</P>
<P>Stewart and <I>The Daily Show </I>stand out because they usually make comedy out of <I>challenging </I>prejudice--picking on those in power, not the less powerful.</P>
<P>Usually. <I>The Daily Show </I>has an infuriating habit of falling back on a boy-they-sure-talk-funny-isn't-their-religion-strange attitude toward Muslims in the Middle East. And they have one response to political protests of any kind--show a picture of the weirdest demonstrators they can find, and lump everyone else in.</P>
<P>Nevertheless, if you want--just for the novelty of it--to see someone on TV pointing out Dick Cheney's war profiteering, or the insane fanaticism of the Republican right, or the seamy, sleazy, sanctimonious racket that passes for "democracy" in this country, you should watch <I>The Daily Show.</I></P>

