<P><font face="Times New Roman, Times, serif" size="4">White House favors for corporate polluters</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>War on the environment</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 2, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WHILE THE Bush administration was carrying out its war on Iraq abroad, its henchmen were organizing a quiet war on the environment at home. The latest attacks include the administration's push for legislation to exempt military bases from environmental laws, and the exemption of the oil and gas industry from compliance with storm-water regulation laws to clean up polluted streams. </P>
<P>And according to an internal U.S. Fish and Wildlife Service document obtained by the <I>Los Angeles Times,</I> the Bush administration says that it won't meet the deadline for 24 court-ordered actions that involve proposing or designating controversial wildlife habitats. Environmentalists say that the delay in complying is an attempt to avoid having to scale back logging, mining and other commercial activities.</P>
<P>In addition, after the Bush administration failed--more than once--to get legislation passed to open up the Arctic National Wildlife Refuge (ANWR) to oil drilling, they've now set their sights on the Beaufort Sea off northern Alaska, which comes within three miles of ANWR. Government officials are inviting oil companies to bid on the rights to drill on 10 million acres in this area--and no congressional approval is needed. </P>
<P>Meanwhile, the Bush administration appears to be purposely losing lawsuits filed by industry groups and corporations. In recent weeks, for example, the White House caved to several industry challenges to the Northwest Forest Plan, which governs logging and habitat protection for salmon and other threatened species. </P>
<P>"I don't know if it's a policy, but it's definitely a pattern," Kristen Boyles, a lawyer for the environmental group Earthjustice, told <I>CBS News</I>. "The industry sues, and then the current administration does a poor job of defending itself or comes to a sweetheart settlement." </P>
<P>Like the settlement announced last week--where the Interior Department agreed to halt all reviews of its Western land holdings for new wilderness protection, and to withdraw protected status from some 3 million acres in Utah. </P>
<P>Still, on Earth Day last month, Dubya had the nerve to issue a statement saying that the government "has an important role to play in protecting our environment" by using "new and innovative policies." What nerve! Bush's "new and innovative" policies are nothing more than a giveaway to big business polluters.</P>


