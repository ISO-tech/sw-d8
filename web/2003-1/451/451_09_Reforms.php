<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Can reforms alone win socialism?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 2, 2003 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>HISTORICALLY, THE main divide in the socialist movement has been between reformists and revolutionaries. Reformists argue for peaceful, gradual, piecemeal change--primarily through the electoral process coupled with moderate pressure from below--as the most "realistic" way to achieve socialism.</P>
<P>The most famous reformists were the German Social Democratic "revisionists" in the early 20th Century (Social Democracy was the name given to socialist parties at the time). Their leading spokesperson, Eduard Bernstein, argued, "The task of social democracy is to organize the working classes politically ...and to fight for all reforms in the State which are adapted to raise the working classes and transform the State in the direction of democracy."</P>
<P>In his view, the task was to build support for socialist candidates, who in turn would use their influence in government to implement a series of social reforms that would overcome capitalism's contradictions.</P>
<P>Wrote Bernstein's most fervent opponent at the time, Rosa Luxemburg, "revisionism does not propose to suppress these contradictions through a revolutionary transformation. It wants to lessen, to attenuate, the capitalist contradictions." "Revisionism"--named for its revising of Marx's revolutionary doctrine--denigrated the importance of struggle, such as strikes and street protests.</P>
<P>Marx and Engels were not opposed to electoral campaigns. But for them, elections were merely opportunities for revolutionaries to "count their forces, and to bring before the public their revolutionary attitude."</P>
<P>As the first mass socialist party developed in Germany in the 1870s, Marx and Engels were sharply critical of the party becoming "infected with the parliamentary diseases, believing that, with the popular vote, the Holy Ghost is poured upon those elected."</P>
<P>They ridiculed Social Democrats who, while putting off socialism "as an heirloom for their children," focus attention on "all sorts of trifles, tinkering away at the capitalist social order so that at least something should appear to be done without at the same time alarming the bourgeoisie."</P>
<P>They heaped scorn on socialists who saw their task as getting "educated" men elected in order to represent workers' interests from above. Socialism, argued Marx and Engels--as well as Luxemburg--was the "self-emancipation of the working class." Therefore, class struggle--in the form of strikes, street protests and ultimately mass insurrection--was the key to transforming society.</P>
<P>It is important to remember, however, that when they promoted revolution over reform, they were not denigrating the fight for reforms. On the contrary, it was precisely these struggles for immediate demands--over wages, working conditions, political and social reforms--that workers developed the ability to impose their collective will on society.</P>
<P>The mass mobilization that constitutes every great revolution doesn't arise from nowhere. It arises from a series of smaller mass struggles over more immediate demands that, at certain moment, qualitatively spills over into a struggle for the entire direction of society.</P>
<P>"A revolution is necessary," wrote Marx, "not only because the ruling class cannot be overthrown in any other way, but also because the class overthrowing it can only in a revolution succeed in ridding itself of all the muck of ages and become fitted to found society anew."</P>
<P>The ruling class will not give up its power peacefully--gradually or all at once. Therefore, the idea of gradual reform is not a recipe for social change, but a recipe for adaptation to existing society. As Luxemburg concluded, "People who pronounce themselves in favor of the method of legislative reform in place of and in contradiction to the conquest of political power and social revolution, do not really choose a more tranquil, calmer and slower road to the same goal, but a different goal. Instead of taking a stand for the establishment of a new society they take a stand for surface modification of the old society."</P>


