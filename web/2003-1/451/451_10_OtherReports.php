<P><font face="Times New Roman, Times, serif" size="4"><b>Stop the racist death penalty</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sarah Barnes and John Green</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>DAVIS, Calif.--A multiracial crowd of about 150 people attended a panel featuring former Illinois death row prisoner Madison Hobley. Hobley is on a national tour to raise awareness about the injustices of capital punishment and to talk about the urgent need to abolish the death penalty nationally. </P>
<P>Earlier this year, outgoing Illinois Gov. George Ryan pardoned Hobley and three others. Hobley shared his feelings of gratitude towards the family members, lawyers and activists who carried on the struggle for his freedom. </P>
<P>"I was in a box for 16 years," Hobley said to the crowd. "I ate cold oatmeal and warm milk. How can the country continue using capital punishment and still consider itself to be civilized?" Hobley asked. </P>
<P>Robin Hobley, Madison's sister, made a heartfelt plea to the audience. Imagine, she said, how you would feel if your loved one were falsely imprisoned for the murder of his own family. She described her feelings of helplessness--before she found a network of activists to help her fight for her brother's freedom.</P>
<P>Cameron Sturdevant of the Campaign to End the Death Penalty described the struggle ahead for activists in California, where 622 men sit on death row. Attorney and law school professor Millard Murphy called for the audience to enter the "battle" to abolish the death penalty. </P>
<P>All of the speakers stressed the importance of activism in the movement to abolish the death penalty.  Robin pointed out that Gov. Ryan wavered in the months leading up to his decision, but consistent activism pushed him to empty his state's death row.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Immigrants' rights</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sarah Grey</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--About 70 people gathered at Union Square in the rain last weekend to denounce the illegal and racist deportations of immigrants.</P>
<P>Ali Raza, of the immigrants' rights group Families for Freedom, detailed his own ordeal for the crowd. "The FBI broke down my door after a neighbor called in a tip that there were Arabs coming and going," said Raza.</P>
<P>Raza was held without charges for six months before deportation proceedings began.  If he loses his appeal, he will be sent back to Pakistan without his family or any way of supporting himself.</P>
<P>Several speakers talked about how Homeland Security's deportations have broken up their families. Many had received a phone call from a missing loved one and found that their husband, parent, or child had been deported, often to a country they had not seen since infancy.</P>
<P>Passers-by joined in with demonstrators' chants of "Deport Ashcroft! Deport Bush!" and "INS: You go home!"</P>


