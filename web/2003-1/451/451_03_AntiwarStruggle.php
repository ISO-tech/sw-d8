<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>New phase of the antiwar struggle</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 2, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WHAT'S NEXT for the antiwar movement? This question naturally emerged among activists after the U.S. victory in Iraq. Of course, the media's pro-war cheerleaders--especially the former-liberals-turned-born-again-bombers among them--had an answer: Shut up and admit you were wrong.</P>
<P>But why? The antiwar movement didn't oppose the war on Iraq because we thought the U.S. would lose. In fact, every reason for standing up against war has been proven correct by the U.S. victory--from the fact that there are still no signs of "weapons of mass destruction," to the growing resistance to the U.S. occupation.</P>
<P>But the corporate media's abysmal coverage of the war has distorted these facts for millions, which is why many people--even those who protested the war when it began--now have a mix of ideas. Some, for example, are confused about what to say about the U.S. occupation. Others accepted the media's stage-managed pictures of Iraqi celebrations of "liberation" and now wonder whether the war at least accomplished some good.</P>
<P>So the first job of the antiwar movement after the war is to cut through Washington's lies and tell the truth--just as we did before. There are still widespread doubts about the Iraq war--and opposition to the Bush administration's openly stated plan to use Iraq as a stepping stone for more wars in the Middle East and beyond. But many people don't know what they can do about this. That's why it's crucial that antiwar organizations maintain activity that can give a voice to this opposition.</P>
<P>For one thing, the U.S. attack on Iraq isn't over. It has entered its next phase--a brutal occupation--and we have to respond to all of its injustices and horrors. Just as importantly, it will be vital to have organizational continuity for the next time the U.S. goes to war--so that activists trying to stop a new slaughter won't have to reinvent the wheel to build a new movement.</P>
<P>A core of people became determined opponents of the U.S. war machine as a result of the struggle against the Iraq war. This core--more sizeable than any movement has produced in decades--can maintain the fight against the occupation, while strengthening our ability to challenge the next U.S. war.</P>
<P>An important part of this will be discussions among antiwar activists about political questions and strategies for organizing. The Campus Antiwar Network, for example--a coalition of high school and college antiwar groups--held regional conferences across the country last weekend and this one, with the goal of emerging as a stronger and more committed organization. Likewise, the national conference of United for Peace and Justice, scheduled for early June in Chicago, can be a step forward in the discussion about where our movement is headed.</P>
<P>No one expects the antiwar movement to draw the tens of millions worldwide that it did in the run-up to the war, or the nearly equal numbers who protested as the invasion started. But we can build a movement that gives voice to the widespread questioning and anger at Washington's war makers and their policies--while preparing for the struggles to come.</P>


