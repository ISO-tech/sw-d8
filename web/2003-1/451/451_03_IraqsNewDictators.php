<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Washington tolerates no opposition to its military rule</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Iraq's new dictators</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 2, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"THE IRAQI people will choose their own leaders," George W. Bush declared April 28. Unless they're opposed to the U.S. occupation, that is.</P>
<P>Bush's comments before an audience of Arab Americans came just days after Defense Secretary Donald Rumsfeld said that Iraq wouldn't be permitted to have an Islamist government, even if the Shiite Muslim majority wanted one.</P>
<P>"A vocal minority clamoring to transform Iraq in Iran's image will not be permitted to do so," Rumsfeld said. "We will not allow the Iraqi people's democratic transition to be hijacked by those who might wish to install another form of dictatorship."</P>
<P>That's because Rumsfeld prefers his own form of dictatorship in Iraq--U.S. military rule fronted by a "vocal minority" of wealthy exiles and former supporters of Saddam Hussein's repressive regime.</P>
<P>U.S. officials were surprised by the size of the Shiite pilgrimage to the holy city of Karbala, where more than 1 million people attended religious ceremonies--many of whom joined demonstrations chanting anti-U.S. slogans. Having endured long years of repression--including the brutal suppression of a 1991 uprising after the Gulf War, while U.S. troops stood by--Shiite clerics have built up a widespread following.</P>
<P>It was U.S. fear of Iran's Shiite Islamist government after the 1979 revolution that led Washington to back Saddam Hussein in the Iran-Iraq War of the 1980s. Today, support for the Shiite clerics has forced Washington to move cautiously.</P>
<P>U.S. troops did, however, force the removal of the de facto mayor of the town of Kut, Said Abbas--who Washington claims is a member the Iran-backed Supreme Council of the Islamic Revolution in Iraq. And U.S. officials have warned Iran not to "interfere" in Iraq--as if the U.S. invasion doesn't count as "interference." The corporate media is parroting the U.S. line that the only choice for Iraq is between a U.S. client state and a conservative Islamist government.</P>
<P>Nevertheless, the display of Islamist influence in Iraq has put even many opponents of the war on the defensive. Some even accept the argument that a U.S. occupation government is a lesser evil compared to the restrictions on women's rights and civil liberties that they say would be imposed by an Islamist government.</P>
<P>Others accept the argument that chaos in Iraq requires a U.S. presence. For example, the group Voices in the Wilderness--which helped to lead the decade-long fight against economic sanctions on Iraq, and whose representatives courageously stayed in Baghdad during the war--accepts this logic.</P>
<P>"It will not serve the tremendous human need in Iraq for the U.S. military to immediately withdraw without a legitimate international presence to take its place," Voices' leaders wrote in April. "From what we've witnessed, this would create a power vacuum that could precipitate the implosion of Iraq's civil society. The U.S. military should be pulled back from its role as a foreign occupation power into a protective role sufficient to allow for Iraq's social and political concerns to be dictated by Iraqi parties."</P>
<P>But the only "protective role" Washington has in mind is grabbing the world's second-largest oil reserves for the benefit of American corporations and asserting U.S. imperialist dominance in the Middle East.</P>
<P>Other opponents of the war argue that the best possible outcome in Iraq is a United Nations (UN) role in the occupation. But the UN's leading role in military occupations of Bosnia and Kosovo provided a cover, behind which ethnic cleansing continued.</P>
<P>Even if the UN took a greater role in Iraq, the end result is still a "regime change" imposed by a foreign power by military means. Democracy in Iraq will never come on the bayonets of U.S. troops or out of the boardrooms of American oil companies.</P>
<P>During the Vietnam War, Washington justified its support for dictatorial regimes in the South to stop the spread of "communism"--the pretext for U.S. military interventions around the world in that era. Now, the U.S. is using the threat of terrorism and Islam to justify a new agenda.</P>
<P>The antiwar movement of the 1960s came to reject Washington's lies and demand complete U.S. withdrawal from Vietnam. Today's antiwar movement has to demand that U.S. troops get out of Iraq--now. Only then can the Iraqi people have genuine self-determination to create the society and government that they want.</P>

