<P><font face="Times New Roman, Times, serif" size="4">On Iraq...At home...</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Stop Bush's Wars</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 2, 2003 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THEY STRIPPED them naked, burned their clothes and wrote "sinful thieves" in Arabic with a black marker across their chests. Then they threw them into the streets. This is how U.S. soldiers in Baghdad punished four young Iraqi men accused of stealing light arms from an Iraqi Army stockpile.</P>
<P>"Ziad said he was so angry at being humiliated by the soldiers that the only thing he wanted to do was find a grenade and throw it at the American soldiers," reported the Norwegian newspaper <I>Dagbladet</I>.</P>
<P>But while the U.S. military was dreaming up new ways to humiliate Iraqis, the real thieves were just arriving. Like Washington's chosen puppet, Iraqi National Congress leader Ahmed Chalabi, already living it up at Baghdad's most luxurious hotels. And Chalabi--who fled Jordan to escape a 32-year jail sentence for embezzling--is a small-time crook compared to the real Washington kingpins.</P>
<P>The man in charge of running Iraq, retired Gen. Jay Garner, has more experience in arms dealing than diplomacy, as president of the defense contractor SY Coleman. George Schulz, the former secretary of state under Ronald Reagan, helped his company--international construction giant Bechtel--to win a $680 million contract to "rebuild" Iraq.</P>
<P>Meanwhile, Brig. Gen. Robert Crear was watching the first crude oil pumped since the war at a well outside Basra. "Now we're in the oil business," crowed Crear, according to the <I>Chicago Tribune</I>.</P>
<P>Meanwhile, back at home, George W. Bush's rich pals are lining up at a different trough. They're looking forward to pocketing most of the $726 billion that Bush wants to hand out in tax cuts. To pay for this giveaway, Bush's new budget proposal cuts spending on almost every government program that poor and working people depend on--including $15 billion sliced out of the Veterans Administration, even as U.S. soldiers are at war for oil in Iraq.</P>
<P>In the name of "national unity" after September 11, Bush asked workers everywhere to tighten their belts--and tighten they did. When American Airlines bosses threatened bankruptcy during contract negotiations last month, they asked union members to take $1.8 billion in concessions. Little did workers know at the time that CEO Donald Carty and 44 other executives had created a special trust fund to protect them if the company went belly-up.</P>
<P>But no one wrote "sinful thief" on Carty's chest. The corporate pirates are on a plundering spree around the world--and workers everywhere have an interest in stopping them. We have to organize the fight to stop Bush's wars--on Iraq and at home.</P>


