<P><font face="Times New Roman, Times, serif" size="5"><b>Say no to Bush's war on Iraq</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Naveen Jaganathan and Elizabeth Schulte</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>STUDENT ACTIVISTS in the Campus Antiwar Network (CAN) met last weekend at regional conferences in California, Rhode Island and Texas to discuss next steps in opposing Bush's war and occupation.</P>
<P>At Brown University, more than 70 students from 18 universities attended--each school sending two elected delegates. Authors and activists George Capaccio and Anthony Arnove addressed the opening session, dismantling the media lies about the "liberation" of Iraq.</P>
<P>Capaccio described the resistance to U.S. occupation and argued that activists need to organize for a long-term struggle. "Iraq is only the beginning in Washington's plans to dominate the world," he said.</P>
<P>Debates arose over whether activists should call for an immediate withdrawal of the U.S. from Iraq and support some form of a United Nations (UN) presence. Delegates voted to adopt two new points of unity for the group, which read, "We stand opposed to unjust wars. We stand opposed to the occupation of Iraq." An additional point of unity for the East Coast was adopted which read "CAN stands opposed to acts of U.S./UN-led aggression."</P>
<P>Delegates voted to organize a national educational speaking tour to jumpstart their antiwar committees in the fall and to produce educational literature over the summer.</P>
<P>On the West Coast, some 60 people from 12 schools came out for the CAN conference held at Chico State University. At the Southern regional CAN conference at the University of Texas at Austin, about 20 people attended. Chicago will be the location of the Midwest CAN conference on May 3-4.</P>
<P>In Chicago, about 300 people gathered on April 27 in a protest organized by Andersonville Neighbors for Peace and endorsed by more than 30 groups and local businesses. "Some Iraqis say that the only thing they've liberated us from is the notion that the United States would save us," said Voices in the Wilderness' Kathy Kelly, who just returned from Iraq.</P>
<I><P>Warren Craig and Sophie Hand contributed to this report.</P>
