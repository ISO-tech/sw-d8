<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHICH SIDE ARE YOU ON?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The looting of Iraq's oil wealth begins</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 2, 2003 | Page 7</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH is calling for the immediate lifting of United Nations (UN) sanctions that have killed more than 1 million Iraqis--a demand raised by human rights activists since the last Gulf War ended in 1991.</P>
<P>But there is no reason to celebrate Bush's sudden change of heart. The U.S. is worried about the well-being of Iraq's oil industry, not its people. The UN sanctions program is the only obstacle remaining for U.S. oil companies lining up to profit from Iraq's newly "liberated" oil reserves.</P>
<P>The U.S. is hoping to sideline the UN by introducing a resolution to the Security Council that ends the sanctions and transfers Iraq's oil wealth to an "interim" Iraqi government, handpicked by the U.S. occupiers.</P>
<P>All the other pieces are in place to get the oil riches flowing again. They already have begun flowing, in fact, through a 38-mile pipeline in southern Iraq that was miraculously spared from damage while U.S. and British bombs destroyed the rest of the country.</P>
<P>Iraq's oil wells also survived the bombing largely intact, and will be ready to begin producing oil within a few months. The Ministry of Oil was the only government building to escape bombing during the war, and the only site protected by U.S. troops from looters ransacking the rest of Baghdad immediately after.</P>
<P>Iraq's oil infrastructure was spared so that the real looting of Iraq could begin. The U.S. has already announced that it will run the Iraqi oil industry just like a corporation, complete with an American CEO and a board of directors. The CEO they have in mind is Philip Carroll, the former chair of Shell.</P>
<P>With Iraq in shambles, food and water shortages plaguing the population, and cluster bomblets still exploding in poor neighborhoods, the Bush administration is shamelessly doling out war booty to a select handful of its closest corporate cronies. </P>
<P>The Bush dynasty's ties to the oil industry are well known, and it is no surprise that the corporations winning Iraqi reconstruction contracts have links to the Republican Party. But there is also a stunning overlap between the architects of the Bush Doctrine of "pre-emptive" war--whether from the Pentagon's Defense Policy Board (DPB) or think tanks like the Project for a New American Century (PNAC)--and the corporations benefiting the most from regime change in Iraq.</P>
<P>The same neo-conservative hawks who hyperventilated for years about ridding the world of Saddam Hussein are now busy funneling billions to corporations in which they have past or present interests.</P>
<P>Halliburton, the oil conglomerate, and Bechtel, the construction giant, are the two firms that landed the heftiest contracts from the $100 billion Congress approved for the reconstruction of Iraq. Vice President Dick Cheney, a founding member of PNAC and a prominent member of the American Enterprise Institute (AEI), was Halliburton's CEO until 2000. George Shultz, Ronald Reagan's Secretary of State and now chair of the right-wing Committee for the Liberation of Iraq, is another supporter of the AEI--and a former CEO and present board member of Bechtel. DPB member Gen. Jack Sheehan is Bechtel's senior vice president.</P>
<P>Defense Secretary Donald Rumsfeld, a founding member of PNAC, also holds a seat on the government's Committee on Foreign Investment. While not on Bechtel's payroll, Rumsfeld visited Iraq as Reagan's special envoy to the Middle East in 1983, to try and convince Saddam Hussein on Bechtel's behalf to build an oil pipeline from Iraq to Jordan. Andrew Natsios, head of the U.S. Agency for International Development, justified the top-secret bidding process for reconstruction contracts that shut out foreign competitors on the basis of "national security"--as he handed key contracts to Bechtel, a company with which he also has a past relationship.</P>
<P>"Ideology is ideology, but in the U.S. government, political theory goes hand-in-hand with big business," wrote Neil McKay in Scotland's <I>Sunday Herald</I> newspaper. "The world isn't just watching the spread of a political philosophy in Iraq, it is watching a conquest by and for U.S. big business as well."</P>


