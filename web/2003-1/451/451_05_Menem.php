<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ARGENTINA</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>How Menem came out on top</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 2, 2003 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IN AN election marked by a lackluster campaign and low voter turnout, former Argentine President Carlos Menem won the first round of a special presidential vote.</P>
<P>Menem got just over 24 percent of the vote, while Nestor Kirchner, governor of the state of Santa Cruz, won 22 percent to join Menem in the second round of voting May 18. Both are members of the Partido Justicialista, known as "Peronists" after the party's founder, the populist general and president, Juan Per&oacute;n.</P>
<P>The election was called by President Eduardo Duhalde, another Peronist, who became the fifth president in two weeks following the Argentinazo, the mass uprising of December 2001. The Argentinazo was the culmination of years of protests against catastrophic free-market policies implemented by Menem in the 1990s and continued by former President Fernando de la R&uacute;a, who was driven out of office by mass demonstrations.</P>
<P>Duhalde had hoped to put off a new presidential vote long enough to build a campaign for re-election. But his failure to solve Argentina's worsening crisis--some 57.5 percent of the population now lives under the poverty line--forced Duhalde to call early elections and step aside.</P>
<P>Duhalde--a former deputy president under Menem--backed Kirchner, who sounded the traditional Peronist populist themes in promising to help workers and the unemployed. Menem's campaign centered on "law and order"--including a military crackdown on protests. This was an appeal to the most reactionary elements in Argentine society--including its former military dictators, who ran the country from 1976 to 1983. Menem also has the backing of big business.</P>
<P>But the differences between Menem and Kirchner are not nearly as great as they seem. It was Duhalde, Kirchner's sponsor, who allowed a police crackdown on the occupied Brukman textile factory, one of more than 100 factories run by workers since employers shut them down during the economic crisis.</P>
<P>The far left, while rooted in Argentina's social struggles, failed to provide a united alternative in the election. Some called for a boycott, while the Workers Party and the United Left ran candidates, with the United Left getting 1.8 percent of the vote.</P>
<P>Whatever the outcome of the second round, Argentine capitalists will use the election to try to claim legitimacy for more austerity. The challenge for the left will be to build a united opposition in the struggles ahead.</P>


