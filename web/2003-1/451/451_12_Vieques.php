<font face="Times New Roman, Times, serif" size="5"><b>Navy pushed out of Vieques</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 2, 2003 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>ROBERTO BARRETO reports from Puerto Rico on the long struggle to stop the U.S. Navy from using the island of Vieques for target practice.</P></B>
<P>THE PEOPLE of Vieques have won an important victory over the U.S. Navy. After six decades of struggle--including an intensive period of mobilizations and mass civil disobedience actions over the past four years--the Navy was forced to abandon Vieques.</P>
<P>On May 1, Camp Garc&iacute;a--which the Navy used as a practice range for decades, using all kinds of weapons, including bombs, napalm and depleted uranium munitions--is set to finally close down for good.</P>
<P>The Navy fought long and hard to stay in Vieques. In April 1999, a stray practice bomb killed a civilian security guard at the base, sparking an outraged response from thousands of people who poured onto the grounds, blocking the use of the shooting range and demanding an immediate and permanent end to military exercises.</P>
<P>Under the slogan "Not one more bomb," 12 civil disobedience camps were set up inside Camp Garc&iacute;a, and the base was effectively shut down. But in May 2000, the Navy organized a huge multi-agency operation to arrest the protesters and retake control of the shooting range.</P>
<P>Protesters continued to enter the camp over the next three years. More than 1,700 persons were arrested--and some were sentenced to up to six months in prison.</P>
<P>In the end, the popular resistance was too much for the Navy. "Physical security at Vieques is becoming ever more difficult and costly to maintain given the civil unrest which accompanies the Navy's presence on the island," wrote Adm. Vern Clark, chief of Naval Operations, in a memo about the closing of the base. "We have been successful in completing our training on the island only because of extremely aggressive and costly multi-agency security actions. The level of protests, attempted incursions and isolated successful incursions generally remains high when Battle Group training occurs on the island."</P>
<P>On May 1, the people of Vieques will celebrate by removing hundreds of feet of the military fence around Camp Garc&iacute;a. Their demand now is for the Navy to take responsibility for decontaminating the land.</P>
<P>Residents of Vieques suffer from numerous illnesses and health conditions as a consequence of the Navy base--including the highest cancer rate in Puerto Rico. So far, the Navy has offered only $2 million to clean up the area. Estimates of the cost to remove the contaminants range into the hundreds of millions.</P>
<P>That is why the struggle for Vieques is not over. The people of the island won't even get to control the land of the former base. The Navy is transferring control to the Interior Department and the Federal Fish and Wildlife Service. Nevertheless, the people of Vieques are determined to continue their fight until their land is decontaminated and returned to them.</P>
<P>Another unexpected consequence of the Vieques struggle has been the closing of most of the Roosevelt Roads Base in Ceiba, Puerto Rico, and the shutdown of installations for U.S. Southern Command. Around 2,500 troops stationed in Roosevelt Roads have received their transfer orders. The Pentagon understands that without the shooting range in Vieques, the Roosevelt Roads base loses its usefulness.</P>
<P>Roosevelt Roads is the Navy's second-largest base and has been used to launch interventions around the Caribbean--for example, the 1983 invasion of Grenada. Its partial dismantling will lessen the U.S. government's ability to use Puerto Rico as a springboard for military interventions.</P>
<P>The lessons of the struggle in Vieques are very important for antiwar activists. This fight shows that ordinary people can organize to stop the war plans of the most powerful empire in the world.</P>

