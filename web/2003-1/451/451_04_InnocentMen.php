<P><font face="Times New Roman, Times, serif" size="4">Thrown in jail for a murder they didn't commit</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"This is like a crime"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 2, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P></B>
<P>Last December, Gildo Teixeira was quietly released from a Rhode Island prison after serving five years for a crime he did not commit. Two other codefendants still languish in prison.</P>
<P>Joa Resendes was stabbed to death on December 27, 1997, during a fight outside a Providence nightclub. The only reason that Teixeira and his two codefendants, Jorge Depina and Joa Monteiro, were ever charged with the murder was because of mistaken accusations by one of the stabbing victims.</P>
<P>Once Providence police had several men in custody, they decided to build a case against Teixeira, Depina and Monteiro. From the beginning, witnesses were inconsistent, giving wildly different descriptions of the accused and of events on the night of the murder. </P>
<P>During one lineup, the defendants overheard a detective tell a witness, "Just pick any three of them." One detective told Monteiro, "Tell us who did it, or you go down for it." All defendants were questioned without a lawyer present. </P>
<P>In the five-year history of this case, witnesses' names, evidence such as the victim's shirt, and even court documents are missing or lost. There is no physical evidence connecting the defendants to the crime.</P>
<P>Gildo Teixeira recently told me that he suspects witnesses were intimidated and manipulated. One of the victim's relatives testified that she had seen Depina stab Resendes, but outside court, she told a friend that she hadn't seen the incident.</P>
<P>Even after the trial, when the appeals court finished hearing the legal arguments, the prosecution approached Teixeira with a total of five deals--which he eventually turned down, since they required him to implicate Depina and Monteiro.</P>
<P>Teixeira, Depina and Monteiro are Cape Verdean-American. The jury was all white and middle aged. "They wanted to release me quietly," said Teixeira, so there wouldn't be any press attention on this case. But Teixeira will not be silent about his and his codefendants' innocence. </P>
<P>Currently, the state is trying to retry Teixeira for murder, and Jorge Depina has had his appeal denied. Maria Depina, Jorge's mother, says that conditions at the Adult Correction Institution, where her son is being held, are deplorable.</P>
<P>"This is like a crime," says Depina. "My son is not eating the food there. Every time he tries, he throws up." Jorge has had medical treatment and is on a liquid diet because he was losing so much weight, she says.</P>
<P>Maria also vows to keep fighting until there is justice. "I want this to go on until everything that they did comes out," Maria says. "They tried to keep Gildo's mouth shut, and they took away his job, his school, and now they don't want to pay!"</P>
<B><P>John Osmand, </B>Providence, R.I.</P>

