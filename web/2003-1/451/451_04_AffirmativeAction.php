<P><font face="Times New Roman, Times, serif" size="5"><b>How bosses use racism to keep us apart</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 2, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Eduardo Nera wrote a letter criticizing <I>Socialist Worker</I>'s analysis of affirmative action for not taking into account that there are poor white people and rich Black people (April 25). While there clearly are poor whites and rich Blacks, Nera goes on to state, "If we are to view the world as Marxists, we must view it as a division of social classes, and not races."</P>
<P>This is where I disagree. Just because Marxists have a class analysis of society doesn't mean we ignore the issue of racial divisions. To the contrary, we view racism as a product of the capitalist system. </P>
<P>Capitalists exploit race, gender and other divisions to divide the working class. Thus, even though all poor people are at a disadvantage when applying for schools or jobs, we recognize that Black people suffer additional discrimination. We also understand that this is not caused by individual cruelty or ignorance, but by a system that profits by keeping people at odds.</P>
<P>Affirmative action is a much-needed way to combat the racism embedded in our system. However, we as socialists recognize that in order to eliminate racism altogether, we will have to get rid of the system that produces it. </P>
<P>Until then, to ignore the issue of race would not only mean ignoring the reality we live in, but would also be a complete misunderstanding of Marxism. If we really wish to build class unity, we must be the most militant antiracists out there.</P>
<B><P>Leela Yellesetty,</B> New Haven, Conn.</P>

