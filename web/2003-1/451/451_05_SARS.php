<P><font face="Arial, Helvetica, sans-serif" size="2"><b>CHINA</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Government lurches into action after ignoring the deadly epidemic</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>How the SARS crisis spread</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By David Whitehouse</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 2, 2003 | Page 5</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>AS THE worldwide death toll from SARS passed 330 this week, the epicenter of the crisis moved from Hong Kong and southern China to the capital of Beijing.</P>
<P>After ignoring the epidemic for months after its outbreak late last year, the Chinese government responded last week with a flurry of measures. First came an admission that cases of SARS--which stands for Severe Acute Respiratory Syndrome--in Beijing numbered 774, instead of the 37 reported a week before. Then, officials ordered the quarantine of more than 4,000 Beijing residents--and the construction, in just five days, of a 1,000-bed isolation facility in a northern suburb.</P>
<P>The disease, caused by a new virus related to the common cold, has spread to 20 of China's 31 provinces and municipalities. China accounts for half of the world's 5,200 confirmed SARS cases, and more than one-third of the country's new cases are in Beijing.</P>
<P>China's oppressive labor system is likely to accelerate the spread of the disease. Workers in the suburbs of major cities, including 3 million around Beijing alone, come from the countryside to work in long-term "temporary" jobs. Like Black workers under South African apartheid, China's "migrant" workers are paid low wages and lack the social benefits enjoyed by permanent residents--including access to health care.</P>
<P>Beijing has offered free SARS treatment to poor residents, but as the <I>Economist </I>magazine noted, few take up the offer. "Many with SARS-like symptoms would think twice about any offer of free treatment, since their ailment may well turn out to be something else for which they would have to pay. Compounding this fear is the risk that days of quarantine for themselves and family members could cause a big loss of earnings."</P>
<P>Beijing's train stations have been jammed with workers trying to flee the epidemic. Many will spread the virus as they return home. Air travelers have carried the disease to two dozen other countries. In North America, the outbreak is centered in Toronto, Canada, where 21 people have died.</P>
<P>The Chinese government last week fired the minister of health and the mayor of Beijing for covering up the epidemic, but the real responsibility lies elsewhere. Top officials of the central government were fully aware of the epidemic in March, but they didn't want bad news to overshadow the installation of China's new president Hu Jintao.</P>
<P>Earlier, the provincial government of Guangdong in the south of the country kept the news of the initial outbreak from health officials. China's biggest magnet for foreign investment is Guangdong province--with millions of "migrant" workers employed in textile and assembly sweatshops.</P>
<P>U.S. toy makers Hasbro, Mattel and Disney, for example, have major operations in Guangdong, and the province recently attracted several big-ticket investors like Honda. Provincial officials in supposedly "socialist" China kept quiet about the outbreak--because they thought that publicity would be bad for the bottom line.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Background on the virus</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SARS CAUSES flu-like symptoms of high fever, aches and dry cough, sometimes leading to difficulty in breathing--and, in more than 5 percent of cases, to death. The virus may have arisen by mutation in animals, which jumped to humans. A high proportion of the earliest cases was among food-service workers.</P>
<P>The SARS virus, like the common cold, may survive for a time in droplets outside the body, but it doesn't spread as easily as cold germs. In the last month, most new cases have come through close contact in hospitals--or through contact with especially infectious individuals known as "super-spreaders." Researchers haven't yet learned what makes somebody a super-spreader.</P>
<P>Labs in several countries are working on a vaccine, but nobody expects results for a year. In the meantime, the main hope is for a standardized treatment that can cut the death rate. Treatments include antiviral drugs and steroids, but they vary widely in doses and proportions.</P>


