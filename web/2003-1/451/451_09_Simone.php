<P><font face="Arial, Helvetica, sans-serif" size="2"><b>OBITUARY: NINA SIMONE</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Songs that rallied the civil rights fight</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Lalasz</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 2, 2003 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NINA SIMONE, who died last week at the age of 70, is widely known as one of the most influential jazz singers--but also as a protest singer and civil rights activist. </P>
<P>Simone's drive for justice was shaped by her youth in the Jim Crow South. She was born in 1933 in Tryon, N.C, one of eight children in a poor family. At the age of 10, at her first recital, she not only experienced her first applause but her first encounter with racism when her parents were removed from the first row to accommodate some whites.</P>
<P>Her musical abilities enabled Simone to leave the South, but racism followed her. In 1950, she studied at the Julliard School of Music in New York City, hoping to become a classical pianist. She began singing after she was turned down for a scholarship to Philadelphia's Curtis Institute because she was Black.</P>
<P>Along with friends, authors James Baldwin and Lorraine Hansberry, Simone became involved in the 1960s fight for civil rights. Her songs, such as "Young, Gifted and Black," became a powerful part of the movement. After the killing of civil rights leader Medgar Evers and the 1963 Birmingham church bombing, Simone wrote "Mississippi Goddam" a furious indictment of racism.</P>
<P>In "Backlash Blues," she sings: "Mr. Backlash, Mr. Backlash/Just who do you think I am/You raise my taxes, freeze my wages/And send my son to Vietnam/You give me second class houses/And second class schools/Do you think that alla colored folks/Are just second class fools/Mr. Backlash, I'm gonna leave you/With the backlash blues." </P>
<P>Embittered by racism in the U.S., Simone left in 1969 to live in Africa and Europe until her death. Her protest songs are a living testimony to the civil rights movement--and the struggle against racism today.</P>

