<font face="Times New Roman, Times, serif" size="5"><b>U.S. holding children at Camp X-Ray</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 2, 2003 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>U.S. MILITARY officials admitted last week that they were imprisoning three children between 13 and 15 years old as "enemy combatants" at the U.S. prison camp in Guant&aacute;namo Bay, Cuba. The revelation represents a disgusting new low for the Bush gang.</P>
<P>Last year, Defense Secretary Donald Rumsfeld told reporters that detainees at Camp X-Ray "have been brought here because they are considered individuals that ought not to be out on the street with the possibility that they could kill somebody else." Turns out, he meant children, too.</P>
<P>The fact that teenagers can be flown thousands of miles from their homes and thrown behind bars in a place that Britain's <I>Daily Telegraph</I> described last year as resembling "a Second World War prison camp" is sickening enough.</P>
<P>But because the U.S. won't classify Camp X-Ray detainees as "prisoners of war"--which would entitle them to certain rights under the Geneva Convention--the children will remain locked up indefinitely, without access to a lawyer.</P>
<P>Angela Wright of Amnesty International called the youths' detentions "wholly repugnant and contrary to basic principles of human rights." Of course, the Bush administration seems to know a lot about policies that are "contrary to human rights."</P>
<P>In the U.S. last week, Attorney General John Ashcroft showed his own contempt for civil rights, ruling that undocumented immigrants can be detained indefinitely--if officials say that their release would "endanger national security."</P>
<P>Under the ruling, Ashcroft denied bail to an 18-year-old Haitian refugee who had come ashore in Florida in October with 216 others. Immigration judges had approved the release of about 100 of the refugees, but Ashcroft overruled the judges, saying that their release "would tend to encourage further surges of mass migration from Haiti by sea, with attendant strains on national and homeland security resources."</P>
<P>Clearly, the land of the free doesn't want any more "undesirables." What's especially frightening is that the ruling may apply not just to those seeking asylum in the U.S., but to almost any undocumented immigrant picked up in the U.S.</P>
<P>Whether it's at home or abroad, the Bush gang is prepared to trample on our rights. We have to stand up and say no to these outrages.</P>


