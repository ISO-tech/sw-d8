<P><font face="Times New Roman, Times, serif" size="4">U.S. claims about Iraq's weapons of mass destruction</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Lies they told to get their war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 2, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SHOOT FIRST, ask questions later. That sums up the Bush administration's approach to war on Iraq.</P>
<P>The U.S. claimed to be going to war because of weapons of mass destruction that it knew Saddam Hussein possessed, yet it couldn't produce any credible evidence to prove it. More than a month after the invasion, there's still no credible evidence.</P>
<P>Last week, chief United Nations (UN) weapons inspector Hans Blix lashed out at the U.S., arguing that the White House undermined his inspectors--who were pulled out of Iraq shortly before the war.</P>
<P>In an interview with the BBC, Blix said that U.S. officials leaked to the media that inspectors had deliberately suppressed information--by failing to release details of a drone and cluster bomb found in Iraq. "At the time the U.S. was very eager to sway the votes of the Security Council, and they felt that stories about these things would be useful to have, and they let it out," Blix said. </P>
<P>Blix also questioned why the U.S. and Britain were so easily duped by faked "evidence" in the case for war on Iraq. Last September, British Prime Minister Tony Blair presented a "dossier" that supposedly "established beyond doubt&#133;that Saddam has continued to produce chemical and biological weapons." The dossier turned out to be a hoax, largely based on an out-of-date Ph.D. thesis, typographical errors and all.</P>
<P>The BBC broadcast Blix's criticism the same day that he made his case before the UN Security Council to send inspectors back to Iraq. Blix also opposed the U.S. plan to lift economic sanctions on Iraq. But the U.S. says that it's in charge of inspections now, not the UN. "Forget it," one State Department official told the <I>New York Times</I>. "On principle, we don't want the United Nations running around Iraq."</P>
<P>Neither, apparently, do they want Iraq under sanctions--the crippling economic embargo that the U.S. pushed through more than a decade ago. That was before the U.S. controlled Iraq, its reconstruction contracts and its oilfields. The hypocrisy of the U.S. demanding that the sanctions--which are responsible for killing more than 1 million people--be lifted would be funny if it wasn't so cruel.</P>
<P>It's possible that the U.S. will still find the "smoking gun"--though much-hyped "discoveries" throughout the war and since have turned out to be nothing. If the U.S. does come up with evidence, the question then will be whether it's real. Whatever the case, Washington's true war aims already lie exposed.</P>


