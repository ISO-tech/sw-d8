<P><font face="Times New Roman, Times, serif" size="4">Bush gets behind Santorum</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Republicans back up a bigot</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 2, 2003 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>JUST WHEN the memory of Trent Lott's racist filth was starting to fade, Sen. Rick Santorum (R-Pa.) reminds us what vile bigots the Republicans are.</P>
<P>Santorum, the third-highest ranking Republican in the Senate, responded to a reporter's question about the U.S. Supreme Court review of a Texas anti-sodomy law by comparing homosexuality to bigamy and incest.</P>
<P>"I have no problem with homosexuality," Santorum raved. "I have a problem with homosexual acts, as I would with acts of other, what I would consider to be, acts outside of traditional heterosexual relationships. And if the Supreme Court says that you have the right to consensual sex within your home, then you have the right to bigamy, you have the right to polygamy, you have the right to incest, you have the right to adultery. You have the right to anything." </P>
<P>When Lott praised Strom Thurmond for his glory days as a Ku Klux Klan-style segregationist, the GOP dumped him as elected leader of Senate Republicans. But proving just how "compassionate" their conservatism is, George W. Bush and other top Republicans are standing behind Santorum.</P>
<P>White House spokesperson Ari Fleischer told reporters that "the president believes that the senator is an inclusive man." And Senate Majority Leader Bill Frist called Santorum "a consistent voice for inclusion." They've got to be kidding. Santorum's record includes not only opposition to gay rights, but also fighting to overturn abortion rights.</P>
<P>"No one who knows the senator is surprised that he made [the remarks]," the <I>New York Times</I> reported. "I suspect Rick doesn't know many gay people, and it may very well be that he never had a serious conversation with gay men and women about why they are gay," his friend, Rep. James C. Greenwood (R-Pa.), told the <I>Times</I>. "I suspect that Rick thinks somehow that people choose for some strange reason to just up and be gay."</P>
<P>Don't expect an apology from Santorum. In an interview with <I>Fox News, </I>he said: "I do not need to give an apology based on what I said and what I'm saying now&#133;These are not, you know, ridiculous, you know, comments. These are very much a very important point." The point being, apparently, that Santorum is very much a very anti-gay bigot.</P>

