<P><font face="Times New Roman, Times, serif" size="4">Labor gives American Airlines $1.8 billion in concessions</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Givebacks won't end crisis</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 2, 2003 | Page 11</font>
<font face="Times New Roman, Times, serif" size="3"><P>ARE AIRLINE workers' wages to blame for the industry's crisis? That's the claim of the industry's CEOs--and, judging by the latest union concessions at American Airlines, labor leaders agree.</P>
<P>The $1.8 billion in givebacks at American came after Donald Carty was forced to resign following the disclosure of a pension fund for top executives that would be protected even if the company went bankrupt. Workers' pensions, by contrast, have no such protections.</P>
<P>Nevertheless, union leaders signed off on the deal even though the rank and file had been kept in the dark about the plan when they voted on the contract. The result is that American's unions followed their counterparts at United Airlines and U.S. Airways--both in bankruptcy--into making massive concessions to try to save those companies.</P>
<P>Workers have only themselves to blame, according to <I>New York Times</I> reporter Steven Greenhouse. "The pilots, the machinists and other airline unions have obtained some of the highest wages in organized labor in decades past, helping push their airlines' operating costs so high that the airlines became vulnerable to downturns and more recently to the emergence of low-cost upstarts like Jet Blue," he wrote in an April 27 article.</P>
<P>In fact, the airline crisis is created by the deregulation of the industry that began in 1978 under Democratic President Jimmy Carter. As economist Robert Kuttner points out, airlines--like public utilities--were regulated because they required enormous capital investment.</P>
<P>Once established, the biggest airlines could easily squeeze out new competitors and use their monopoly power to set fares artificially high. Regulation prevented price-gouging, stabilized the industry and ensured stable and dependable service.</P>
<P>Once regulations were removed, the big airlines were still able to restrict competition--but were themselves subjected to booms and busts of the market. In 1994, for example, workers at United Airlines were pressured into taking nearly $5 billion in concessions in exchange for an employee stock ownership plans. Now those stocks are worthless--and layoffs and concessions are being imposed again.</P>
<P>Airlines tried to succeed in the 1990s by jacking up the price of business fares and buying up smaller or weaker competitors--such as American Airlines' takeover of US Airways. But this only meant that American was stuck with even more overcapacity when business started cutting travel budgets well before the attacks of September 11--a trend that has continued with the recession and weak recovery.</P>
<P>"Face it: There are some industries where the ideal of pure competition doesn't work," Robert Kuttner wrote. "Try to pursue it and you get a crazy quilt of scams, monopolies, bankruptcies, random bargains and opportunistic price-gouging."</P>
<P>Airline unions shouldn't be taking concessions, but fighting to preserve jobs and pay through re-regulation of the industry.</P>


