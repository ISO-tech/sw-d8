<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Bush and his pals scramble to justify a war against Iraq</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Any excuse will do</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 24, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>LAST WEEK'S discovery of 16 empty chemical warheads in Iraq touched off a media frenzy about the lack of "cooperation" with United Nations (UN) weapons inspections. And increasingly, "cooperation" with the inspections--rather than evidence that Iraq possesses "weapons of mass destruction"--is becoming the standard for the Bush administration to justify its plans to go to war against Iraq.</P>
<P>"I think the test is not [weapons]," Defense Secretary Donald Rumsfeld declared last weekend. "The test is [whether] Saddam Hussein is cooperating&#133;he's not doing that. The president said time is running out, and if the test is, are the Iraqis going to cooperate, that's something you're going to know in a matter of weeks, not in months or years."</P>
<P>Why would the administration suddenly stress "cooperation" as a trigger for war just after the first discovery of any weapons evidence by inspectors? Because no matter how hard the Bush administration tries, the idea that 16 empty warheads are a reason for war is stretching it.</P>
<P>After all, even if the shells were filled with chemical or biological agents--and inspectors admit that there is no evidence that they ever were--they wouldn't be a threat to the U.S., or to Israel or even to neighboring countries. Because the warheads have an effective range of 12 miles.</P>
<P>But U.S. officials won't let the truth get in the way of their war drive. With close to 150,000 U.S. troops deployed to the region, the Bush White House has made it clear that it's ready for an all-out assault--and it will use all the means at its disposal, including more "discoveries" of Iraqi weaponry, to get its war.</P>
<P>"America has entered one of its periods of historical madness, but this is the worst I can remember: worse than McCarthyism, worse than the Bay of Pigs, and in the long term, potentially more disastrous than the Vietnam War," novelist John le Carr&eacute; wrote last week.</P>
<P>"Baghdad represents no clear and present danger to its neighbors, and none to the U.S. or Britain. Saddam's weapons of mass destruction, if he's still got them, will be peanuts by comparison with the stuff Israel or America could hurl at him at five minutes' notice. What is at stake is not an imminent military or terrorist threat, but the economic imperative of U.S. growth. What is at stake is America's need to demonstrate its military power to all of us--to Europe and Russia and China, and poor mad little North Korea, as well as the Middle East; to show who rules America at home, and who is to be ruled by America abroad."</P>
<P>Earlier this week, U.S. politicians of every stripe were scrambling to claim a piece of the legacy of Dr. Martin Luther King Jr. "Injustice anywhere is a threat to justice everywhere," Bush had the gall to say, quoting King's 1963 "Letter from a Birmingham Jail."</P>
<P>But you can bet that Bush and the rest of his pals won't be reading from "Beyond Vietnam," a speech that King gave a few years later as the U.S. escalated its assault on Vietnam. In that speech, King denounced the war as an "enemy of the poor" that "was sending their sons and their brothers and their husbands to fight and die in extraordinarily higher proportions relative to the rest of the population." And he committed himself to opposing "the greatest purveyor of violence in the world today: my own government."</P>
<P>Once again, Washington is proving just how cruel and savage its war machine can be. And the antiwar movement can take strength from King's commitment to the struggle against it.</P>


