<P><font face="Times New Roman, Times, serif" size="5"><b>The lies of the first Gulf War</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 24, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>As America rushes to war, I wish someone would broadcast the award-winning documentary that was aired here in Canada following the Gulf War. The Canadian Broadcasting Company (CBC) investigated the so-called "facts" that were presented to the American public to sell them on the idea of going to war.</P>
<P>Remember the young lady who was crying on TV? She claimed to have been working in the hospital in Kuwait when Iraqi soldiers came in and removed babies from incubators--and placed them on the cold floor so that they could "steal" the incubators.</P>
<P>The CBC investigation found that the incubators had been removed from the room they were in and stored in another area of the hospital, because the room they were in was being painted. No babies were removed by soldiers.</P>
<P>And the young lady? She couldn't have been in Kuwait at the time she claimed because she was with her father in New York City. Her father was the Kuwaiti ambassador to the U.S.</P>
<B><P>J.R. Clancy</B>, Courtenay, British Columbia, Canada</P>


