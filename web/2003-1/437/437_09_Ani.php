<P><font face="Times New Roman, Times, serif" size="5"><b>Ani DiFranco's stand against injustice</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by David Thurston</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 24, 2003 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>MUSIC: </B>Ani DiFranco, Righteous Babe Records, 2002--CD: <B><I>So Much Shouting So Much Laughter</B></I>. DVD: <B><I>Render: Spanning Time with Ani DiFranco</B></I>.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>THERE'S A lot to love in two recent releases from Ani DiFranco--especially her forceful opposition to war, racism and injustice.</P>
<P>This fall, DiFranco released a live, double CD, titled <I>So Much Shouting, So Much Laughter,</I> and a DVD called <I>Render: Spanning Time with Ani DiFranco</I>. On <I>Render, </I>Ani offers great music and road-trip footage, but most strikingly, interludes between pieces in which Ani tackles political questions closest to her heart.</P>
<P>In one, she explains why she kept her record label based in Buffalo, N.Y., and captures the devastation that industrial decline brought to the city's Black and working-class population. This puts flesh on a line from "Cradle and All," in which she writes "The Tyco plant went to Mexico/Left my uncle standing out in the cold/Saying here's your last paycheck, have fun growing old." Another standout is a segment titled "Criminal Injustice System," which focuses on the death penalty.</P>
<P>But the most welcome political statement is "Self-Evident," a poem set to music in which she describes the horror of September 11, but comes out resolutely against Bush's wars on Afghanistan and Iraq.</P>
<P>She issues a bold statement of solidarity with the victims of U.S. imperialism: "So here's a toast to all the folks who live in Palestine, Afghanistan, Iraq, El Salvador/ Here's a toast to the folks living on the Pine Ridge reservation under the stone cold gaze of Mt. Rushmore."</P>
<P>She goes on to blast Dubya: "We hold these truths to be self evident: #1 George W. Bush is not president/ #2 America is not a true democracy/ #3 The media is not fooling me." </P>
<P>Even if you can't always agree with her solutions, it's great to have an artist who poses so many of the right questions. And at a time when so many musicians have remained silent about Bush's plan for endless war, DiFranco is a voice for our side.</P>


