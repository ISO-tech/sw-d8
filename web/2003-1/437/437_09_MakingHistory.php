<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Can individuals change history?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 24, 2003 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MOST HISTORY books treat historical change as the accomplishment of great men (and an occasional woman). According to this view, the movers and shakers in history are the Napoleons, Lincolns and FDRs of the world.</P>
<P>This view is also applied to revolutions. George Washington, Robespierre, Lenin--these men shaped history, and the actions of the masses of people in these revolutions were merely events scripted by their leaders.</P>
<P>The only difference between the treatment of Washington and Lenin as great men is that Washington, as a leader of the American Revolution, gets a plus sign in front of his name, whereas Lenin, a leader of a working-class revolution, gets a minus sign. </P>
<P>The opposite, though less popular, view is that history follows a path which no individual can influence--"great men" are merely agents for its realization. According to this view, individuals and their actions are purely products of historical conditions.</P>
<P>Had there been no Napoleon Bonaparte, another figure would have played the same role, because historical conditions in the period of the early 18th century demanded a "Napoleon." "We cannot make history," wrote Bismarck, taking this to its extreme. "We must wait while it is being made."</P>
<P>The first view serves as an ideological justification for the rule by a minority--"great" kings, presidents and leaders have special qualities that give them the ability to rule whereas the rest of the "herd" must follow. But the second view can also serve as a means to justify brutal exploitation and suffering. How can you fault a ruler whose actions are historically determined and therefore beyond his control?</P>
<P>Both of these ideas are mistaken, though they contain elements of truth. There are, for example, a few cases where different scientists working independently of each other made the same discovery--historical conditions were ripe for it.</P>
<P>Individuals do indeed make history. But they cannot influence society or history in any direction they so choose. Individuals cannot exert their will independently of the social conditions in which they find themselves. "Individuals can influence the fate of society," wrote the Russian Marxist George Plekhanov, "by virtue of definite traits in their nature. Their influence is sometimes very considerable, but the possibility of its being exercised and its extent are determined by society's organization and the alignment of its forces.</P>
<P>"An individual's character is a 'factor' in social development," concludes Plekhanov, "only where, when and to the extent that social relations permit it to be."</P>
<P>Many examples come to mind. It may, for example, have been possible for a philosopher in ancient Greece to dream of circumnavigating the globe, but the technology and knowledge for such a voyage did not exist until the 15th century.</P>
<P>An early Christian may have dreamed of a society free of exploitation where wealth is shared, but only with the development of modern capitalism have the material conditions been created which make such a world possible.</P>
<P>For ideas expressed by groups or individuals to become a material force that can affect the outcome of history, therefore, there must be both the objective conditions and the subjective conditions. To put it crudely: if there is not enough food to go around, then my dream of feeding everyone is not realizable. But if there is enough food to go around--and capitalist production has now made that a reality--there still must be the subjective conditions to make a world free of hunger possible.</P>
<P>There must be a level of consciousness and organization among a sufficient number of people to transform social relations and create a new system of production and distribution. In this scenario, the role of individuals can be decisive at certain key moments--but only if they are a link in a chain of other factors. I'll come back to this in my next article.</P>


