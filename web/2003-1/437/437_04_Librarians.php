<P><font face="Times New Roman, Times, serif" size="5"><b>Librarians take up fight for civil liberties</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 24, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P></B>
<P>Librarians and booksellers in Vermont have joined hands to overthrow provisions of the USA PATRIOT Act that threaten to undermine Americans' freedom to research and read in private. </P>
<P>Rep. Bernie Sanders (I-Vt.) held a press conference December 20 to announce that, on the basis of the librarians' and booksellers' efforts, he will be introducing legislation to challenge those sections of the law that threaten to erode a citizen's right to read free of government intrusion.</P>
<P>The USA PATRIOT Act--passed with no public hearings and virtually no congressional debate in a rushed wave of post-September 11 anxiety--gives law enforcement officials authority to demand that a library or bookseller turn over business records, including the records of books that individuals have borrowed or purchased. </P>
<P>The standard of evidence to order that such records be turned over is extremely low; the orders are issued by a secret court; and librarians and booksellers are prohibited by the law to reveal that an investigation is taking place. </P>
<P>Under the secrecy provisions of the law, it will be impossible to monitor the government's use--or potential abuse--of these provisions, since the existence of such investigations are kept secret. "If you have to worry about what your reading list might look like to an FBI agent, you might decide to censor yourself and not read what you really want to read," said Trina Magi, a librarian at the Bailey/Howe Library at the University of Vermont. </P>
<P>We must join with all who are concerned about the erosions of our civil liberties under provisions such as the USA PATRIOT ACT and keep the pressure on to protect our civil liberties.</P>
<B><P>Peter Spitzform, </B>Burlington, Vt.</P>
<I><P>To see the text of the open letter endorsed by the Vermont Library Association, go to http://www.vermontlibraries.org/patriot.html.</P>


