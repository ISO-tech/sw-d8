<P><font face="Times New Roman, Times, serif" size="4"><b>Misunderestimating Dubya</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MAYBE DUBYA could be convicted--of butchering the English language. They may have started out as verbal slip-ups but several of George W. Bush's mangled phrases managed to find their way onto a list of the top words of 2002. </P>
<P>"There are already 11,000 instances of 'misunderestimate' on the Web. The more people use words, whether jocularly or seriously, the more likely they are to enter the language and last for generations," said Paul J.J. Payack, chairman of yourDictionary.com, which compiled the list. </P>
<P>According to Payack, "Our lists attempt to capture those...innovations in word choice and usage that tell us something about ourselves." They include the Bushisms: "misunderestimate" (to seriously underestimate), "embetter" (to make emotionally better--the opposite of embitter), "resignate" (as in "They said this issue wouldn't resignate with the people") and "foreign-handed" (as in "I have a foreign-handed foreign policy"). </P>
<P>And some non-Bushisms made the list as well, like the well-known accounting disease known as "Enronitis," and "dot-communism" (the conviction that everything on the Web should be free.) </P>
<P>--Reuters, January 15, 2003</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Georgia's out of the bedroom</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>UNMARRIED PEOPLE can finally have sex in the state of Georgia--legally, that is. The Georgia Supreme Court recently struck down a 170-year-old law that made it a crime for unmarried people to have sex. The ruling came in the case of a 16-year-old boy tried for "fornication" after his girlfriend's mother caught the couple having sex.</P>
<P>"Our opinion simply affirms that&#133;the government may not reach into the bedroom of a private residence and criminalize the private, noncommercial, consensual sexual acts of two persons legally capable of consenting to those acts," Chief Justice Norman Fletcher wrote.</P>
<P>Following his conviction, Jesse McClure, now 17, was ordered to pay a fine and write an essay explaining why he should not have had sex. He wrote that it wasn't the court's business. And finally, the Court agreed.</P>
<P>--Associated Press, January 15, 2003</P>

