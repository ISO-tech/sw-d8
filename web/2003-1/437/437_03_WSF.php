<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Organizing for another world</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 24, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>MORE THAN 100,000 people are expected at the third annual World Social Forum (WSF) this week in Porto Alegre, Brazil. The meeting will mark the latest stage of the rebellion against free-market "neoliberal" policies across Latin America--and the revival of the left internationally.</P>
<P>The first WSF in 2001 was conceived as an alternative to the World Economic Forum, an elite gathering of corporate bosses and political bigwigs held at the posh resort of Davos, Switzerland. It brought together an unprecedented collection of representatives of unions, peasant and landless workers' groups, social movements and nongovernmental organizations.</P>
<P>During a video-conference debate, global justice activists in Porto Alegre took on top capitalists in Davos. Radical Filipino economist Walden Bello summed up the sentiment when he declared, "It would be good for the thousands of businessmen in Davos to be loaded into a spaceship, and for that spaceship to take off." With the slogan "another world is possible," the WSF inspired similar regional gatherings around the world, from India to Lebanon to Italy.</P>
<P>Last year's forum took place just a month after a mass rebellion that overthrew the government in neighboring Argentina, where corporate globalization and neoliberal policies have led to a 1930s-style economic depression.</P>
<P>This year, the leader of Brazil's Workers' Party, Luiz In&aacute;cio "Lula" da Silva, a former metalworker who was once imprisoned for union activity under a military dictatorship, will address the WSF as the country's new president. Lula embodies the hopes and aspirations, not just of Brazil's workers and poor, but of tens of millions from Mexico to Argentina who are fed up with the misery of more than a decade of free-market policies.</P>
<P>In one of his first acts in office, Lula postponed the purchase of fighter jets to put more money into a new government program to reduce hunger. Yet Lula has also steered the Workers' Party to the right, promising to pay Brazil's enormous $255 billion foreign debt to the International Monetary Fund and Western banks--even though such payments will severely restrict aid to the 22 million poor Brazilians.</P>
<P>Lula's contradictory position is symbolized by the fact that after he speaks in Porto Alegre, he'll depart for Davos--to meet with German Chancellor Gerhard Schr&ouml;der, French President Jacques Chirac and assorted CEOs and bankers.</P>
<P>Real change in Brazil won't come from Lula, but from below--from the grassroots social movements, unions and left-wing organizations. The WSF this week will play a major role in the debate about how to take that struggle forward--and not just in Brazil, but internationally. And as Washington prepares to launch a one-sided slaughter in Iraq, that internationalism is needed more than ever.</P>
<P>Last year's forum in Porto Alegre made the link between the U.S. government's free-market economic policies and its aggressive militarism--as two arms of U.S. imperialism. In the U.S., however, some in the global justice movement have shied away from antiwar activism in an attempt to appeal to more moderate political forces.</P>
<P>But with Bush escalating his war on working people at home even as he prepares a military onslaught abroad, the connections are clearer than ever. That's why the European Social Forum held last November in Florence, Italy, not only challenged neoliberalism, but organized a massive antiwar protest as well.</P>
<P>The World Social Forum will provide an opportunity to deepen this resistance. And at the same time, the movement can carry a message of hope to a world wracked by economic misery and war--that another is world possible, and growing numbers of people are joining the struggle to achieve it.</P>


