<P><font face="Times New Roman, Times, serif" size="4">The Bush gang calls for an end to affirmative action</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>This is an attack on civil rights</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa and Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 24, 2003 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH took the opportunity of Rev. Martin Luther King's birthday to show his support for overturning affirmative action at the University of Michigan--and quite possibly the entire country.</P>
<P>In declaring the administration's intent to file a brief in a Supreme Court case that will be heard later this year, Bush said that Michigan's affirmative action policies amounted to a "quota" system. He called on the justices to declare the university's admissions system unconstitutional.</P>
<P>The White House didn't have to take a position on the case. Nor did Bush have to make the announcement himself--a task usually left to some underling in the Justice Department. By wading into the affirmative action debate so publicly, Bush was sending a message--that the administration is going on a right-wing offensive on many fronts at once, from taxes to abortion to civil rights issues.</P>
<P>The announcement also showed how hollow Bush's criticism of Sen. Trent Lott (R-Miss.) was--after Lott waxed nostalgic for the days of segregation at a birthday party for outgoing Sen. Strom Thurmond (R-S.C).</P>
<P>In his attempt to save his job as leader of the Republicans in the Senate, Lott even promised in an interview on Black Entertainment Television that he would support affirmative action! But just a month later, the Bush gang was again playing to racist prejudice on this hot-button issue.</P>
<P>The Michigan case revolves around the charges of three white students that the university chose not to admit. The two applicants to the undergraduate school were B students in a pool of more than 25,000 applicants for 5,187 slots. Apparently, some Black and Latino high school students with similar or poorer academic records than the three whites were admitted.</P>
<P>But like all arguments against affirmative action, this narrow focus misses the bigger picture--the years of systematic discrimination against minorities that remains a barrier in both education and at work. </P>
<P>Even with the existing affirmative action rules in effect, Blacks and Latinos are grossly underrepresented in the student body at the University of Michigan--about 11 percent of the law school, for example, compared to 17.5 percent in the state's population.</P>
<P>Rather than representing preferential treatment, this seems clear evidence of continued discrimination. In fact, the University of Michigan admissions system awards almost as much preference to applicants from the state's mostly white, rural Upper Peninsula as it does to minorities.</P>
<P>The administration's intervention in this case shouldn't be a surprise. After all, Solicitor General Ted Olson--the man who filed the Supreme Court brief for the Bush administration--worked for the Center for Individual Rights, a right-wing organization representing the white students in the University of Michigan case, before Bush hired him to attack affirmative action from inside the government.</P>
<P>Right-wing fanatics like Olson have long made affirmative action the centerpiece of their attack on the reforms won by the social movements of the 1960s and '70s. But though they disguise their arguments with rhetoric about "fairness," even the corporate media recognized that the administration's attack was designed to pander to racism.</P>
<P>We can't let them get away with turning back the clock on our rights.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>And they talk about equal opportunity?</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE BUSH has some nerve preaching about unfair advantage and equal opportunity. His entire life is an advertisement for why there is no "equal opportunity" or "level playing field" in the U.S. </P>
<P>As a self-described "C-student" whose first paper in prep school earned the comment "disgraceful" from one teacher, it's hard to believe that Bush made it to Yale and Harvard on his own abilities.</P>
<P>The fact that he was the wealthy son and grandson of powerful politicians assured that he got into the nation's top schools--and that he never lost money, even when he ran several businesses into the ground.</P>
<P>But opponents of affirmative action don't get so upset with this kind of "preferential treatment"--the kind that rich, white men have always enjoyed.</P>


