<P><font face="Times New Roman, Times, serif" size="4"><b>Abolish Maryland's death penalty</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mike Stark</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ANNAPOLIS, Md.--Chanting "Hey Ehrlich, just face it! Death row is racist!" more than 75 protesters from across Maryland and Washington, D.C., came out to protest the inauguration of Republican Gov. Robert Ehrlich on January 15.</P>
<P>Ehrlich&#130; Maryland's first Republican governor in 30 years, has pledged to overturn the state's moratorium on the death penalty. This sets the stage for a possible seven executions in 2003, the largest number of executions in one year in Maryland history.</P>
<P>The last execution here was in 1998, when Democratic Gov. Parris Glendening oversaw the execution Tyrone X Gilliam. Protest and public outcry forced Glendening to back off his pro-death penalty position, first by taking Eugene Colvin-El off of death row and then by signing a moratorium on all executions last May.</P>
<P>A recently released two-year study by the University of Maryland of the state's death penalty found that "Blacks who kill whites are 2.5 times more likely to be sentenced to death than are whites who kill whites, 3.5 times more likely than are blacks who kill blacks and almost 11 times more likely to be sentenced to death than 'other' racial combinations."</P>
<P>Ehrlich says he will ignore the study's findings and "examine each case individually." But a case-by-case approach will avoid the very patterns that are so disturbing, such as the fact that each year, Blacks comprise nearly 80 percent of murder victims, yet 100 percent of the people on Maryland's death row are sentenced for killing whites. Also, 67 percent of those on death row are Blacks accused of killing whites, yet Black-on-white crime makes up less than 5 percent of murders each year.</P>
<P>Protesters were inspired by Illinois Gov. George Ryan's recent decision to pardon four death row inmates and to remove the sentence of death from the remaining 167 inmates facing execution. "There's always hope because we saw a great act of courage from [Illinois] Governor George Ryan," said Stephanie Gibson of the Maryland Coalition Against State Executions. "We're going to make this governor hear us."</P>
<P>The protest was organized by the Baltimore and Washington chapters of the Campaign to End the Death Penalty. Former death row inmate Shujaa Graham; sister of Maryland death row inmate Vernon Evans, Evangelist Bates; and the mother of police shooting victim Archie Elliot, Dorothy Elliot, attended the protest. The ISO, the ACLU, Amnesty International, American Friends Service Committee and MD CASE also mobilized for the protest.</P>
<P>This is just the first step in the fight to keep the racist death penalty out of Maryland!</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Protest Kissinger</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Evan Kornfeld</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LOS ANGELES--Winnie McPherson spoke for protesters on January 13 when she declared, "We don't want a war criminal like Kissinger in our city!"</P>
<P>Henry Kissinger, former secretary of state responsible for death and destruction in countries like Chile, East Timor and Vietnam, was invited to speak at the Universal Amphitheatre as part of the University of Judaism's annual speaker series.</P>
<P>Last year, the school invited Bill Clinton and Madeleine Albright, prompting complaints that its speakers were "too liberal." So this year they invited Kissinger in the interest of "fairness."</P>
<P>Several dozen people turned out for the protest organized by the Workmen's Circle/Arbeter Ring Southern California District. Many demonstrators carried signs against the war in Iraq and some brought Palestinian flags.</P>
<P>McPherson, of the Librarians' Guild, AFSCME 2626, also went after Bush's "homeland security" offensive: "They go after union organizers. They go after immigrant workers."</P>
<P>A group of religious bigots showed up to harass protesters. But they were no match for the small, but spirited group of antiwar protesters, who vowed to bring Kissinger to justice. A speaker from the Peace Committee of the Trinity Church had a useful suggestion: "Let's deport Kissinger."</P>


