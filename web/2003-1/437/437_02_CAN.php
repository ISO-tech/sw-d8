<P><font face="Times New Roman, Times, serif" size="5"><b>Campus antiwar groups form network</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 24, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>SOME 300 students made their way to Washington, D.C., and San Francisco a day before the January 18 marches for antiwar conferences that together founded the Campus Antiwar Network (CAN). Representatives of antiwar groups on more than 50 campuses met in Washington, while students from nearly 30 campuses gathered in San Francisco.</P>
<P>The main question facing participants was how to better organize and coordinate the activities of dozens upon dozens of campus-based student coalitions that have sprung up in recent months. The two meetings a continent apart came together with a conference call held via speakerphone to agree on a series of proposals.</P>
<P>Earlier in the day, the students in Washington had adopted CAN as the network's name, agreed to the single point of unity of no war on Iraq and called for a national conference to be held in Chicago on February 22-23.</P>
<P>In the spirit of collaboration that marked the proceedings in both conferences, the West Coast meeting reported that it had voted to accept the same decisions--sending a strong message of unity as the date for the national conference in Chicago approaches.</P>
<P>The East Coast conference opened up with a keynote address by Jeremy Corbyn, a member of parliament in Britain and leading member of the Stop the War Coalition. "United on a basic program, we are very, very strong," Corbyn said. "We can bring people with us, we can unite people around us, we can strengthen the peace and antiwar movement. If we're unable to unite around those kinds of basic demands, people are not going to flock to us and they're going to feel that sense of isolation and despair. They see the world going to war, and they feel they can do nothing about it." </P>
<P>The East Coast students also voted to endorse the February 15 rally in New York City and march as a student contingent there. And CAN groups are planning protests and speak-outs for the first school day after a U.S. invasion of Iraq. CAN's formation marks another important step for the developing antiwar movement.</P>
