<P><font face="Times New Roman, Times, serif" size="4">Resisting Illinois prosecutors' death penalty attack</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>No turning back!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 24, 2003 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>ALAN MAASS reports on the ongoing fight against the death penalty in Illinois.</P></B>
<P>PROSECUTORS AND right-wing politicians across Illinois have launched an all-out campaign to undermine former Gov. George Ryan's emptying of death row. But opponents of the death penalty are determined to stop this attempt to restart the machinery of death--and to pressure lawmakers to abolish capital punishment once and for all.</P>
<P>Ryan sent shock waves across the world earlier this month when he issued a blanket commutation of every death sentence in the state. Coming to office as a pro-death penalty Republican, Ryan called a halt to executions three years ago after mounting pressure followed the freeing of four innocent death row prisoners in the first year of his term--making a total of 13 men to that point who had faced death for crimes they didn't commit.</P>
<P>A commission appointed by Ryan recommended 85 reforms to make the system more "fair"--but the state legislature refused to take action on a single one. So in his final days in office, Ryan took action himself, pardoning four innocent death row prisoners--and commuting the sentences of the other 167 inmates facing execution.</P>
<P>Prosecutors instantly lashed out at Ryan--with Cook County State's Attorney Dick Devine, leading the way. Devine filed two lawsuits against Ryan's action, challenging 13 cases in which prisoners didn't sign clemency petitions and another 10 commutations granted to prisoners who were awaiting new hearings on their sentences. Plus prosecutors in the Chicago suburbs are joining Devine in trying to put former death row prisoners back on trial for their lives by reviving old charges in other cases.</P>
<P>Naturally, this bunch hasn't worked up the same energy for charging the police torturers that Ryan identified as a decisive reason for issuing pardons in four cases and the blanket commutation for others.</P>
<P>Opponents of the death penalty--whose years of organizing exposed the system's injustices and set the stage for Ryan's action--are firing back. Members of the Campaign to End the Death Penalty held two neighborhood forums in Chicago featuring the exonerated death row prisoners--and have set January 31, the third anniversary of Ryan's moratorium, as the date for a town hall meeting on Chicago's South Side to press for abolition.</P>
<P>State Rep. Art Turner and several other lawmakers announced this week that they have reintroduced legislation to abolish the death penalty, and activists say that they will organize petitionings and demonstrations to pressure legislators. Plus, U.S. Rep. Bobby Rush organized a press conference on Martin Luther King day to put pressure on Devine himself.</P>
<P>Last April, an Illinois appeals court judge appointed a special prosecutor to look into the torture of African American suspects by Chicago cops under fired Commander Jon Burge. All four of the prisoners pardoned by Ryan were victims of these torturers, and dozens more people remain in prison convicted mainly on the basis of these coerced confessions. Devine was a top official in the prosecutor's office when the tortures took place. Burge's victims say that Devine and other law enforcement officials were "completely complicit."</P>
<P>These initiatives by death penalty opponents can build on the momentum from the commutations victory. "Death row is empty today not because of the criminal injustice system, but in spite of the criminal injustice system," said Alice Kim, an organizer with the Campaign to End the Death Penalty. "We all have to be on a mission to fight for justice and to fight for abolition."</P>

