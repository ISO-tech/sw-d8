<P><font face="Times New Roman, Times, serif" size="5"><b>Washington state teachers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Steve Leigh, Member, SEIU Local 925</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 24, 2003 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>OLYMPIA, Wash.--Some 25,000 teachers, education workers and supporters rallied here January 14 to demand decent funding for education in what was called the largest rally ever at the state capitol.</P>
<P>More than 100 school districts were shut down--many of them "voluntarily" closed since they knew the teachers would be gone. Besides the rally at the state Capitol, teachers also demonstrated in Spokane and Yakima in eastern Washington.</P>
<P>The rallies and walkouts were organized by the Washington Education Association (WEA) to protest $600 million in proposed cuts in K-12 education. The teachers are especially angry because Democratic Gov. Gary Locke's proposed budget voids two ballot initiatives passed overwhelmingly by popular vote two years ago.</P>
<P>The first initiatve, I-732, mandated automatic cost-of-living increases for education employees every year. The second, I-728, gave extra state money to lower class sizes. Yet Locke is proposing that the legislature "suspend" the initiatives until 2005--because the state "can't afford" them!</P>
<P>At the same time, the state can afford to continue the business tax breaks that take over $2 billion from state coffers every year. The state can also afford to enforce tax-cut initiatives passed by smaller majorities than the pro-education ballot measures. And the state can afford to continue to jail nonviolent drug users.</P>
<P>Locke, the self-proclaimed "education governor," is also proposing draconian cuts in higher education, medical care for the poor and other social services. As one protester, a school counselor from Olympia, said, " I don't like the way our government spends its money. Why should we spend so much on the military when we can't afford money for public education!"</P>
<P>Many teachers warmly supported antiwar signs and slogans. A third-grade teacher from Olympia said "We need to raise the status of education. We need to put the priority on class size and pay." Many saw the attacks on education as part of the overall attack on social services. They wanted a fairer tax structure that could provide funding by taxing the rich.</P>
<P>In spite of these sentiments, the leadership of the WEA just focused on education without much reference to the broader issues. Other groups hurt by the governor's budget are also taking a separatist approach. State workers rallied the day before the teachers. College students opposing tuition increases rallied the day after.</P>
<P>This is a mistake. A successful movement to roll back these savage cuts will need to ally all groups hurt by the governor's budget. The beginnings of this movement could be seen in the support at the rally by other unions. SEIU had a large contingent. Other union contingents included the Teamsters, International Longshore and Warehouse Union and Operating Engineers.</P>
<P>This rally was a giant step forward--but much more solidarity is needed.</P>


