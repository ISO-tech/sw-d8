<P><font face="Times New Roman, Times, serif" size="4">New York cops off to a bloody start in the new year</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>They kill and call it justice</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 24, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P></B></I>
<P>The cops in New York City got the New Year off to a bloody start, killing four young men in the first two days of January. They had the nerve to say that three of the killings "appeared justified" and that the fourth was likely an "accident."</P>
<P>The two men killed on New Year's Day were firing shots into the air in celebration of the new year. On January 2, 17-year-old Alan Newsome was killed for supposedly threatening an undercover cop who was disguised as a restaurant deliveryman with a BB gun, and John Lagattuta was shot for driving a stolen vehicle.</P>
<P>Not surprising, three of the four victims were young men of color, and all four were killed in working-class neighborhoods.</P>
<P>Unfortunately, there has yet to be any sort of organized protest against these murders. For example, Rev. Al Sharpton--who spearheaded the protests after the police torture of Abner Louima in 1997 and the killing of Amadou Diallo in 1999--has taken a wait-and-see attitude. This time, Sharpton called for an independent investigation into the killings. According to a National Action Network representative, private investigators are currently at work to "find out what happened." </P>
<P>But the Harlem residents chanting "Martial law in Harlem" immediately after the Newsome killing have already made up their minds. We don't need "experts" to tell us what we already know: the cops harass and kill minorities in poor and working-class neighborhoods--and call it justice. We have to stand up and protest every time a cop kills in this city.</P>
<P>Sharpton also told the crowd, "Because of bad cops, good cops have to answer questions." But it is not an accident, nor the fault of a few "bad apples," that the cops continuously target the most vulnerable and oppressed people in our society. The cops are not here to protect us but to terrorize the poorest sections of society and keep us from uniting to fight back.</P>
<B><P>Sarah Hines, </B>New York City</P>

