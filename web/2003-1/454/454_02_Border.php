<P><font face="Times New Roman, Times, serif" size="4">Washington's anti-immigrant policies take a deadly toll</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Nightmare in Texas</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Quent Reese</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 23, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">NINETEEN UNDOCUMENTED workers from Mexico and Central America are dead after being trapped in the back of a stifling, unventilated truck trailer during a trip through Texas.</P>
<P>The 19 were among about 60 immigrants who had packed into the trailer near the Mexican border for the drive to Houston, where many had jobs and family. But the trip soon became a nightmare. The refrigeration unit in the cargo area apparently was never turned on. As the trailer heated up, the desperate passengers scratched at the insulation and tried to signal to anyone outside. </P>
<P>But by the time driver Tyrone Williams pulled into a truck stop south of Houston in Victoria, it was too late. Seventeen people were found dead at the scene--among them a 6-year-old boy--and two more died later in the hospital. </P>
<P>Other passengers were treated by the Red Cross at the Victoria Community Center or local hospitals--where, disgracefully, federal agents turned away relatives desperate to find out if their loved ones were alive. Now, the survivors of this tragedy face deportation proceedings--after, that is, they are used as witnesses in the investigation into the tragedy.</P>
<P>Authorities have already settled on a scapegoat--the driver, Tyrone Williams, an immigrant from Jamaica himself who was paid $5,000 by smugglers to drive the human cargo. Both state and federal prosecutors are threatening to seek the death penalty.</P>
<P>This is sheer hypocrisy. The terrible death toll in Texas last week is still only a fraction of the hundreds of "deaths by border"--as the Mexican government calls them--that take place each year. And the main cause of this epidemic is the anti-immigrant policies of the U.S. government.</P>
<P>Since the mid-1990s, when the Clinton administration launched its get-tough policy on illegal immigration, the number of people who die trying to get into the U.S. has skyrocketed by 500 percent. The reason, U.S. officials freely admit, is that increased border patrol presence in urban areas has forced illegal immigrants to cross through more rugged terrain--such as the high mountains of California and the deserts of Texas. </P>
<P>As one Immigration and Naturalization Service supervisor told the <I>San Diego Union-Tribune</I> in 1996, "Eventually, we'd like to see them all out in the desert." This callous disregard for human life--when no one disputes the consequences of such policies--shows the truth about law enforcement officials and politicians who place the blame for immigrant deaths on "human smugglers."</P>
<P>What's more, stepped-up law enforcement efforts along the Southwest border since September 11 have driven smugglers to use still more dangerous routes and methods, putting even more lives at risk. </P>
<P>Before September 2001, the Bush administration was considering proposals to grant some degree of amnesty to illegal immigrants--and to create a guest worker program that would allow many Mexicans to work legally in the U.S. for short periods. All that disappeared after 9/11--and more recently, Mexico's failure to "get on board" with the U.S. war on Iraq ruffled many politicians' feathers in Washington. Earlier this moth, in fact, the House International Relations Committee declared that Mexico should not get an immigration agreement unless it opened its state-owned oil industry to U.S. investment.</P>
<P>Washington's priorities are clear--for money and business leaders to cross borders freely, while people can't. Until this changes, more people will die trying to find a better life in the U.S.</P>
<P>We have to say no to the crackdown on immigrants--and demand that the borders be opened now.</P>

