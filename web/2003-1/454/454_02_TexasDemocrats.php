<P><font face="Times New Roman, Times, serif" size="4">Runaway Democrats win</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Republican scam stopped in Texas</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Cindy Beringer</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 23, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">DEMOCRATIC STATE lawmakers returned to Texas to a hero's welcome last week after fleeing the state to successfully defeat a Republican power grab. The Democrats caused an international sensation when they high-tailed it to Oklahoma to stop Republicans in the state House of Representatives from having a quorum for a vote on congressional redistricting.</P>
<P>The runaway Democrats were the butt of plenty of jokes. But even the mainstream media had to recognize that the Republican proposal Democrats were trying to stop was a brazen scam.</P>
<P>The redistricting legislation--concocted by U.S. House Majority Leader Tom DeLay, known to his fellow Republicans by the affectionate nickname "The Hammer"--redrew the map for U.S. congressional districts in Texas. DeLay's plan would have given Republicans an almost guaranteed lock on seven more seats in Congress.</P>
<P>Manufacturing that kind of swing meant drawing some truly absurd boundaries--like one new district, for example, that was 300 miles long, yet only 100 <I>yards </I>wide for long stretches.</P>
<P>When the Democrats' boycott stayed solid, frantic Republican Gov. Rick Perry dispatched the Texas Rangers state police with orders to slap the cuffs on fugitive lawmakers. But because the Democrats had crossed state lines--setting up shop in a Holiday Inn in Ardmore, Okla.--the Rangers were powerless.</P>
<P>That didn't stop the cops from harassing family members and legislative staffers left behind in Texas. Incredibly, DeLay apparently tried to get the FBI to intervene in the dispute--and the involvement of the Department of Homeland Security in helping to track down the Democrats is now the subject of an investigation.</P>
<P>The Democrats returned when the time limit for passing the redistricting plan expired late last week, leaving the proposal dead for the time being.</P>
<P>Unfortunately, Democrats haven't been as enthusiastic about fighting Republican attacks on human services, carried out in the name of balancing a budget that is $15 billion in the red. But seeing DeLay's flagrant scheme for stealing political power go down to a humiliating defeat was a refreshing change of pace.</P>

