<P><font face="Times New Roman, Times, serif" size="4">St. Louis police raid homes of activists</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Crackdown on dissent</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 23, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">PRE-EMPTIVE POLICE crackdowns on protesters and left-wing activists from California to the Midwest are violating civil liberties and threatening the right to dissent.</P>
<P>In St. Louis, activists planning to protest at the World Agricultural Forum (WAF) found themselves the target of a massive police raid on their homes and organizing center May 16.</P>
<P>City officials say that they shut down the Community Arts and Media Project because of building code violations. But the arrests of more than a dozen people were designed to harass those attending an educational conference and protests against the WAF, which is backed by agribusiness giant Monsanto.</P>
<P> "The police engaged in a pre-emptive strike that had been planned days in advance, using a subterfuge of a building inspection to ransack the building and arrest the occupants for loitering," Justin Meehan, the activists' attorney, told <I>Socialist Worker.</I> "We were told the primary purpose of the raid was a search for weapons. What police found were items easily explainable, [such as] nails needed for building rehab and fire batons for a circus troupe. There was also one bottle that had a rag in it--put there by whoever seized it--that police called a Molotov cocktail."</P>
<P>The protests went on as planned, with an estimated 500 people taking part in a peaceful demonstration as police, outnumbered protesters.</P>
<P> "From Friday to Sunday, St. Louis was a police state," Meehan said. "This is basically a flexing of patriotic muscles at the expense of civil liberties--a pre-emptive strike to deprive people of their basic constitutional right to assembly. Even after the event was over, police continued to stop, detain and search individuals who were leaving the demonstration, or who appeared to be demonstrators."</P>
<P>Meanwhile, the <I>Oakland Tribune </I>reported that similar pre-emptive action was behind the vicious police attack on an antiwar picket at the Port of Oakland docks April 7. In that protest, police fired rubber bullets at close range at protesters and longshore workers, causing serious injuries.</P>
<P>The cops had been briefed by the California Anti-Terrorism Information Center (CATIC), which had monitored e-mail by protesters and longshore union activists. "[I]f you have a protest group protesting a war where the cause that's being fought against is international terrorism, you might have terrorism at that [protest]," CATIC Mike Van Winkle told reporters. "You can almost argue that a protest against that is a terrorist act."</P>
<P>The American Civil Liberties of Union of Northern California called Van Winkle's comments "shocking."</P>
<P>Left-wing activists in the university town of Champaign, Ill., found themselves the target of pre-emptive action as well, when city authorities cited building code violations to shut down the Independent Media Center May 8--without giving the group an opportunity to comply with citations made just days earlier. Similarly, the Hot House, a Chicago performance space and bar that frequently hosts progressive political meetings, was also closed for alleged code violations.</P>
<P>These crackdowns have received little play in the mainstream media. We need to speak out against these violations of our civil liberties--and assert our democratic right to protest.</P>

