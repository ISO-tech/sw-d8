<P><font face="Times New Roman, Times, serif" size="5"><b>Making teachers pay for the budget crisis</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 23, 2003 | Page 4</font></p>

<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>A massive heist is happening in Rhode Island. Like many states, Rhode Island is facing a budget shortfall. This has prompted the new Republican Gov. Donald Carcieri to call for reductions in teacher and state employee pensions, as well as for concessions from state employees' unions. </P>
<P>This request is particularly perverse in light of that fact that the state brings in more money from gambling than it does from all taxes on business combined. </P>
<P>One of the two main gambling establishments is located in the Providence suburb of Lincoln. Lincoln is known for its corrupt town officials and its brand-new luxury housing subdivisions--and now, its overcrowded schools. Despite all the newfound local wealth, Lincoln High School is at 122 percent capacity, and its middle school is at 139 percent (The state recommends that schools be at no more than 85 percent capacity). </P>
<P>So on May 9, 60 students picketed the town hall in favor of a ballot resolution to raise $700,000 in local tax money to add "modular classrooms"--trailers--to alleviate the overcrowding. </P>
<P>If this resolution fails, the only option is for the schools to institute a double shift, operating from early morning to early evening--and eliminating all extracurricular activities. Similarly, the Providence public schools just lost $6 million from next year's budget, to make up for the $59 million deficit in the city. Two hundred employees have been laid off, and sports, art and music programs cut back or eliminated. </P>
<P>It is nothing short of a crime when kids suffer for budget deficits, while massive corporations like Fidelity Investments and Brown University--through deals they have cut with state and local governments--get away without paying a penny in taxes. </P>
<B><P>Brian Chidester, </B>Providence, R.I.</P>

