<P><font face="Times New Roman, Times, serif" size="4">Republicans push new attack on D.C. schools</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>The voucher rip-off</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 23, 2003 | Page 4</font></p>

<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</I>, </P>
</B><P>In an outrageous turn of events, Washington, D.C.'s Democratic Mayor Anthony Williams recently announced his support for school vouchers. </P>
<P>This is just the latest in a series of attacks unleashed by the mayor, the city council and school board on public education funding in the District. In the chronically under-funded D.C. Public Schools (DCPS) system, politicians frequently tout half-baked plans that are based on teachers and students paying for the crisis. </P>
<P>But the voucher scheme is one of the worst ideas to date. The idea behind vouchers is that students could opt out of a bad public school by using a voucher, paid for by money taken from the public schools budget, to pay for going to a better private school.</P>
<P>But most estimates are that a voucher program would assist only 7 percent of students in the D.C. public schools. In D.C., tuition costs for many private schools are nearly $20,000 a year--far more than the vouchers would be worth. That's why vouchers would really only benefit upper- and middle-class families--which have enough personal income to cover the rest of the cost of private school tuition.</P>
<P>Meanwhile, vouchers divert desperately needed funds from all public schools. This sets up more public schools in D.C. to fail.</P>
<P>It's no surprise that the plan for vouchers for D.C. originated with the Bush administration. In February, George W. Bush first proposed a $75 million nationwide voucher pilot program, which would include DCPS. Since Congress controls the D.C. budget, politicians hope to use their leverage to force unpopular schemes down our throats.</P>
<P>Now, Williams is telling reporters that he is "willing to try an experiment." He has spent the past six years cutting taxes for the rich and slashing funds for social programs across the board. </P>
<P>We can't let him get away with this next wave of attacks. Teachers, parents and students need to organize to defeat the plans for vouchers and to demand full funding for public education now!</P>
<B><P>Michele Bollinger, </B>Washington, D.C.</P>

