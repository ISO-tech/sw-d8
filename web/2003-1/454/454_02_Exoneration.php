<P><font face="Times New Roman, Times, serif" size="5"><b>Exonerated after 18 years on death row</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 23, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">AFTER SERVING 18 years for a murder he didn't commit, John Thompson walked off Louisiana's death row earlier this month. Thompson became the 108th death row prisoner exonerated since the death penalty was reinstated in 1976.</P>
<P>His case is typical of the barbaric injustice of America's killing machine. In 1984, when he was just 22 years old, John was convicted and sentenced to death for robbing and murdering hotel executive Ray Liuzza. Over the next decade and a half, Thompson came within weeks of being executed several times.</P>
<P>Then, in 1999, weeks before another execution date, a defense investigator found a crime lab report that cleared Thompson. Blood from the assailant found on the victim's pants didn't match Thompson's blood type. Dying of colon cancer, Orleans Parish prosecutor Gerry Deegan confessed to a friend that he had made sure the swatch from Liuzza's pant leg and tennis shoe stained with blood never made it to the trial. </P>
<P>In 2001, John's sentence was reduced to life without parole, and last year, he was finally granted a new trial. By this point, police had "lost" the murder weapon, and several eyewitnesses had come forward to say that he was not the killer. After hearing all this, it took the jury less than an hour to return a not-guilty verdict in the new trial.</P>
<P> "John Thompson's case highlights the need for a moratorium on executions," said Jen&eacute; O'Keefe, executive director of the Moratorium Campaign in a statement. "This is not the first time someone wrongfully convicted has fallen through the cracks in our system. And it probably won't be the last."</P>
<P>The authorities haven't given up on harassing John. Despite robbing him of 18 years of his life, prosecutors have charged him with "aggravated battery" for allegedly hitting a prison deputy with a pair of handcuffs last year. If convicted, he could face as much as 10 more years in prison.</P>

