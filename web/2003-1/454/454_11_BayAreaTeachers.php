<P><font face="Times New Roman, Times, serif" size="5"><b>Bay Area teachers fight budget cuts</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jessie Muldoon</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 23, 2003 | Page 11</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>OAKLAND, Calif.--Teachers here have protested budget cuts at several school board meetings, but at the May 15 meeting, the protests took a major step forward. Hundreds of people representing the many unions at the Oakland schools packed the boardroom. The Service Employees International Union, the Building Trades Council, Teamsters and the Oakland Education Association (OEA) stood together in an impressive show of solidarity. </P>
<P>Before a March 15 deadline, 1,076 teachers were given layoff notices, and while many were rescinded, the district stands to lose up to 600 teachers through layoffs, early retirement and attrition. In addition, the school board approved about 260 layoffs of non-teaching staff, including food service workers, aides to the handicapped and instructional assistants. "How can you sleep at night?" OEA President Sheila Quintana demanded of the board. </P>
<P>The meeting was so contentious that Oakland police were posted at the entrance to the administration building and even formed a line between the school board and the protesters. The meeting was effectively shut down and re-scheduled for the next morning at 7:30 a.m.</P>
<P>Through last-minute organizing, hundreds again packed the boardroom. Quintana led the room in chants of "No layoffs, no cuts!" as she sat on the school board's podium.</P>
<P>The situation is particularly urgent for the teachers' union whose contract expired more than a year ago. The district wants to freeze wages on top of a 6 percent cut, and the OEA's offer--which is already a major concession--is no raise for three years plus big increases in benefit costs to members. The district has declared an impasse, which makes a strike legal. Teachers are planning a series of actions to keep the pressure on the school district.  </P>
<P>The protest at the school board showed that our strength is solidarity. All the unions need to keep the pressure on and move forward with the momentum that we've built.</P>

