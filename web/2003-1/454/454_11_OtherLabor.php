<p><font face="Times New Roman, Times, serif" size="4"><b>Oakland dockworkers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By John Gallagher</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>OAKLAND, Calif.--About 300 activists marched to the Port of Oakland and shut down operations for the night, costing the Stevedoring Services of America (SSA) and other war profiteers millions of dollars. Chanting "We're going to the docks, standing up to the cops!" activists also brought attention to the April 7 antiwar march during which Oakland police fired wooden dowels and other projectiles to scatter demonstrators. </P>
<P>Port of Oakland officials told members of the International Longshore and Warehouse Union (ILWU) not to come to work in order to "avoid" any confrontation like April 7. But ILWU member Clarence Thomas condemned this hypocrisy. "Today's return to the docks is a clear signal that people will not be intimidated!" said Thomas.</P>
<P>Other activists joined the march in a show of solidarity. "We're here because those things they did to you on April 7 are things they've been doing in our community year after year," community activist Gwen Hardy of People United for a Better Oakland told the <I>San Francisco Chronicle.</I> </P>
<P>Although port officials strategically shut down operations, this proves that activism can force the powers-that-be to back down.</P>

