<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 23, 2003 | Page 11</font></p>

<P><font face="Times New Roman, Times, serif" size="4"><b>Cintas</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lauren Fleer</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--Demonstrators, some dressed as coffee cups, marched and rallied this week to protest Starbucks' recently signed contract with laundry giant Cintas. </P>
<P>Cintas employs 1,000 workers in the Chicago area and 17,000 nationwide. In recent weeks, protesters have demonstrated in cities across the U.S., including New York, Philadelphia, Detroit, Las Vegas and San Francisco, as part of a national organizing campaign by the Union of Needletrades, Industrial and Textile Employees (UNITE).</P>
<P>Cintas is the continent's largest uniform rental provider and industrial laundry, controlling more than 30 percent of the market. It has lucrative national contracts with corporations such as Chevron, Exxon, Firestone, Ford, General Motors, Delta and UPS.  </P>
<P>More than 5 million workers wear a Cintas uniform every day. "And these are hard working, well-paid union members," remarked UNITE organizer Pete DeMay. "The Cintas label is stabbing them in the back. Cintas stands against everything they've fought for." UNITE is asking union locals to press their employers not to renew their contracts with Cintas.</P>
<P>While Cintas has grown steadily in sales and profits--now making five times the revenues it did 10 years ago--Cintas employees still work in unsafe conditions, bring home a pittance and are unable to afford health insurance for their families. While the company's $2.27 billion in sales and $234 million in profits provide executives with fat salaries, Cintas workers make between $6 and $11 hourly and pay as much as $180 per month for health care with a $400 deductible.</P>
<P>More than 40 lawsuits have been filed against the company for racial, sexual, age and disability discrimination, and the National Labor Relations Board has issued more than 25 complaints against Cintas for its violations of federal labor law. And a $100 million class-action lawsuit is currently pending against Cintas for cheating workers out of tens of millions of overtime wages.</P>
<P>But so far Cintas has escaped these legal challenges largely unscathed. While Cintas persists in using surveillance, captive audience meetings and firings to intimidate and coerce workers out of union organizing, UNITE is stepping up the pressure against them. Look for an action in your area.</P>
