<p><a name="Cocaine"></a><font face="Times New Roman, Times, serif" size="4"><b>Cocaine sentencing rules are still racist</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P><B>Dear <I>Socialist Worker,</I> </B></P>
<P>I am writing you after reviewing the recommendations that were filed to Congress dealing with the discrepancy between cocaine and "crack" cocaine sentencing policies. Being a family member of a first-time nonviolent drug offender, I was very disturbed by the results. </P>
<P>What troubles me and many others is the recommended 20-to-1 ratio of jail time for "crack" cocaine vs. powdered cocaine. If the previous 100-to-1 ratio is unfair, then a 20-to-1 ratio must also be unfair. The concern is great, because even if sentencing is changed to a 20-to-1 ratio, there will still be tens of thousands of nonviolent drug offenders who will not feel any relief.</P>
<P>To my understanding, these laws were enacted mainly for "drug kingpins." But ask any Black or Latino who was been convicted either with conspiracy or possession of 5 or 10 kilograms of "crack" or cocaine if they consider themselves a "kingpin." The answer is no!</P>
<P>We need a cure and soon, or else we will have a huge problem to deal with in the near future.</P>
<P><B>Christina Stephens, </B>Olathe, Kan.</P>

