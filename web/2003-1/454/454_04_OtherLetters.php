<P><font face="Times New Roman, Times, serif" size="4"><b>Sweatshops on wheels</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P><B>Dear <I>Socialist Worker,</I> </B></P>
<P>The Department of Transportation (DOT) and Federal Motor Carrier Safety Administration (FMCSA) have just made U.S. interstates a more dangerous place to work for America's 3 million truck drivers. With the new Hours of Service rules, the FMCSA has insured that more fatigue-related accidents and deaths will occur.</P>
<P>On the surface, it might look like they are allowing drivers to get more rest, by cutting the on-duty period from 15 hours with 10 hours driving followed by eight hours rest, to 14 hours with 11 hours driving followed by 10 hours of rest. But I can tell you from experience that 10 hours of interstate driving is mind-numbing and fatiguing enough, and 11 hours is even more so.</P>
<P>Most truck drivers now exceed the maximum 70 hours on-duty time in eight days as it is, leading to fatigue and accidents that caused 755 fatalities and 19,705 injuries each year, according to the DOT.</P>
<P>The hours revamp was supposed to address this problem. But after eight years of "looking at the problem, the new rules only make matters worse. Under the new system, a driver can log 364 hours in 31 days--84 more hours of work than under the current system.</P>
<P>America's trucks are truly becoming sweatshops on wheels. Instead of doing something to relieve the unending hours of overtime or to create more jobs in the midst of a recession, the government just handed trucking bosses the right to ratchet up the exploitation of their current workforce.</P>
<P>This didn't have to happen. While there are 3 million truck drivers on the road, there is an industry-wide shortage, and driver turnover for many companies is at or over 100 percent per year.</P>
<P>While the International Brotherhood of Teamsters now represents around 25 percent (and falling) of drivers, it could have used the hours of service issue to organize a campaign for driver safety and fought for rules that would improve drivers' lives--not the bottom line. Hoffa and Co. blew it.</P>
<P>It's now time for all truck drivers--union and non-union--to fight back against the DOT's new "Hours of Slavery."</P>
<P><B>Leighton Christiansen, </B>Riverside, Iowa</P>
