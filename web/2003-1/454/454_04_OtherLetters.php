<p><a name="Priorities"></a><font face="Times New Roman, Times, serif" size="4"><b>These priorities are all screwed up</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P><B>Dear <I>Socialist Worker,</I> </B></P>
<P>Beginning this summer, students at the University of North Carolina-Greensboro (UNCG) will have to start paying for every page they print on the university's computers. According to the administration, students are beginning to print too much for the university to "foot the bill" anymore. </P>
<P>Many students are extremely angered by the university's decision to start charging for printing, because they thought those costs were covered in the $100 "Education and Technology" fees they pay each year--along with an array of other mandatory fees.  </P>
<P>This new system could create a huge problem at our school. UNCG is a largely commuter-based, working-class campus, and many students simply can't afford to buy computers or pay for printing elsewhere.</P>
<P>The question that needs to be asked, though, is why the university can't "foot the bill." The real problem is the budget crisis in education that is plaguing cities across the U.S. There is a disgustingly large amount of money spent on the military in this country, and much too little spent on education. </P>
<P>Instead of spending billions of dollars on a war on Iraq that looked more like a bloodbath than liberation, the government should be giving money to schools for printing costs, school lunches, decent pay for teachers and many, many other things.</P>
<P>The priorities of this system are completely screwed up. That's why we need to unite--workers and students alike--to fight back against Bush's war at home and abroad and create a real challenge to the system as we know it.</P>
<P><B>Julie Southerland, </B>Greensboro, N.C. </P>
