<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>William Bennett: A windbag deflated</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 23, 2003 | Page 9</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>IT'S ALWAYS a delight to see a right-wing blowhard getting caught up in the web of his own hypocrisy. And it's especially delightful when the blowhard in question, former drug czar William Bennett, has made his reputation on lecturing ordinary Americans about living a virtuous life.</P>
<P>While lecturing the rest of us about the need for self-control, Bennett blew as much as $8 million at Las Vegas and Atlantic City casinos over the last decade.</P>
<P>Socialists don't really care that Bennett or anyone else gambles. That's why we're not about to criticize Bennett in the way that writer Joshua Green did in the June <I>Washington Monthly</I>.</P>
<P>Green condemned Bennett for setting the bad example of engaging in an activity its critics link to alcoholism, spousal and child abuse. Those criticisms sound a lot like Bennett's moralizing. In any event, unemployment and low pay cause more stresses on ordinary Americans than playing the lottery does.</P>
<P>The real scandal of gambling is that it has become an excuse for many states and localities to shirk their responsibilities to tax corporations and the rich to pay for public education and other essential services. Promises that gambling will provide a windfall for state budgets or thousands of new jobs prove illusory, according to Jennifer Vogel's 1997 <I>Crapped Out</I>.</P>
<P>Bennett's relationship with the leading gaming establishments provides the Bennett saga's most telling revelations.</P>
<P>Leading casinos considered Bennett a "preferred customer," supplying him with limos to help him get around without having to rub elbows with the less famous. They extended him credit (not a bad bet for them, since Bennett seemed to be a pretty lousy gambler) and they communicated with him "discreetly" so as not to call attention to his frequent gambling trips. Bennett's high-rolling lifestyle sounds a lot like those of other Vegas "players," like sports stars and entertainment moguls that Bennett has made a career of bashing. </P>
<P>Bennett behaved like a member of the "cultural elite" because he is a member of the cultural elite. In fact, he confirmed this when he engaged in one of the cultural elite's most hackneyed public relations ploys--the ritual public apology--an act for which the pre-fallen Bennett had no sympathy when others did it. This is where the biggest hypocrisy and scam of the Bennett story is to be found.</P>
<P>If conservatives tried to win elections on their strength of appeals of tax cuts for the rich, they would have the votes of the richest 10 percent of the electorate. To get ordinary people to vote for them, they have to win millions to vote against their own economic interests.</P>
<P>One very successful way of accomplishing this has been the right's appeal to "morality." They urge large numbers of ordinary people who feel uneasy about their lives--making ends meet, coping with deteriorating schools, worrying about crime--to vent their frustrations against the "liberals" who they say are responsible for the problem. Making a bogeyman out of the "cultural elite"--as if Barbra Streisand or hip-hop artists are more responsible for destruction of ordinary people's lives than the Enrons and WorldComs--has been central to the right's strategy.</P>
<P>Bennett has been one of the most successful purveyors of this nonsense. A failed academic who knew enough to quote Aristotle and Plato to give his GOP talking points a scholarly gloss, Bennett advanced the right's cultural agenda--and made himself a millionaire in the bargain. As Ronald Reagan's Education Secretary and George Bush I's drug czar, Bennett pushed "tough love" crackdowns that have weakened public education and have swelled the prison population to more than 2 million.</P>
<P>We can only hope that Bennett will crawl under a rock so that we don't have to listen to him anymore. But if fellow cultural warriors Jerry Falwell and Pat Robertson can get away with calling the September 11 attacks God's punishment for U.S. tolerance for secularism and homosexuality, we shouldn't bet on it.</P>

