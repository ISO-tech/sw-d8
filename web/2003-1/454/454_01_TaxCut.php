<P><font face="Times New Roman, Times, serif" size="4">Giving away more loot to his rich pals</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bush's tax cut fraud</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 23, 2003 | Page 1</font></p>

<P><font face="Times New Roman, Times, serif" size="3">GEORGE W. BUSH is trying to get away with highway robbery. Again. And the politicians in Washington--Republicans <I>and </I>Democrats--are helping him do it. Again.</P>
<P>Two years ago, Bush paid off his super-rich campaign contributors with a $1.35 trillion package of tax breaks--and the richest 1 percent of U.S. taxpayers got away with nearly half of the loot.</P>
<P>Less than two years later--despite a stagnating economy, worsening long-term unemployment and a rapidly expanding government budget deficit groaning under the weight of massive new military spending--Bush and his buddies are back for more.</P>
<P>Last week, the House and Senate passed separate versions of Bush's proposal, both reducing the size of the giveaway. But congressional negotiations are likely to give the administration almost everything that it wanted.</P>
<P>At red-white-and-blue photo ops held around the country this month, Bush repeated the White House justification for this scam--that tax cuts will spur the economy and create jobs. Really? So why have 2.7 million jobs been <I>eliminated </I>since Bush took office, despite the $1.35 trillion tax cut?</P>
<P>The mumbo jumbo about creating jobs is a lie. The real purpose of the administration's proposal is to hand out as much money as possible to the very richest Americans.</P>
<P>But that's not all. Bush's tax cuts are consciously designed to put the federal government in a financial straitjacket for decades to come. The White House is preparing the ground for politicians to plead poverty to any call for increased government spending. Unless, of course, the call comes from the Pentagon brass for more weapons of mass destruction.</P>
<P>With attention focused on the U.S. invasion of Iraq, the Bush gang have been carrying out a guerrilla war on the government programs that working people need most desperately--from funding for our schools, to Medicaid and Medicare, to assistance for the poor.</P>
<P> "The Bush administration's stealth attack on the poor," author and doctor David Hilfiker wrote, "is camouflaged in 'minor' regulatory changes, routine reauthorizations, 'voluntary' block grants, budgetary complexities and other arcana...Place the many pieces on the table together, however, and the breadth and the depth of the attack become startling."</P>
<P>And yet there's no outcry in official Washington. The Democrats may be pleased that they reduced Bush's latest tax cut proposal. But they should be demanding that Washington repeal the first tax cut--and use the money to help working people suffering through one of the worst economic slumps since the Great Depression.</P>
<P>We can't expect much from the politicians--unless they feel the pressure to act. That's why we have to build on all the struggles, large and small, throughout U.S. society to organize a real challenge to Washington's Robin-Hood-in-reverse policies.</P>

