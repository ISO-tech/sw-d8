<P><font face="Times New Roman, Times, serif" size="4">MIT investigates whether tests were faked</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Bush's Star Wars fraud</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 10, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>JUST DAYS after George W. Bush announced an acceleration of the Pentagon's missile defense system, a leading university said that it was investigating whether "Star Wars" was a fraud.</P>
<P>Officials at the Massachusetts Institute of Technology (MIT) bowed to pressure from one of its faculty members, Timothy Postol, a former Reagan administration arms specialist who has long argued that the U.S. government--with help from schools like MIT--rigged test results for the anti-missile program during the late 1990s.</P>
<P>According to Postol, a trial run of rockets that were supposed to knock out incoming ballistic missiles showed that the rockets, made by defense contractor TRW, couldn't distinguish a real missile from decoys.</P>
<P>Either TRW was lying, Postol contends, or MIT's Lincoln Laboratory covered up for the company by pronouncing that the tests were legitimate. Until now, MIT officials were reluctant to investigate--perhaps because the Lincoln Laboratory is their biggest source of federal grants.</P>
<P>This scandal highlights the cozy connection between government, universities and weapons manufacturers. For example, Boeing, the country's second-largest defense contractor, recently hired Darleen Druyun, a former top Air Force official in charge of arms purchases, to help run Boeing Missile Defense Systems. In her new job at Boeing, Druyun will know just what to say to keep the U.S. military brass happy--like her counterparts at TRW did.</P>
<P>Bush's accelerated missile defense system will cost an extra $1.5 billion over the next two years, on top of $16 billion already slated for the project. And by placing the first installations in Alaska, the White House sent a threatening message to North Korea that fueled the current crisis over that country's nuclear program. What's more, Bush had to tear up the 1972 Anti-Ballistic Missile Treaty with Russia in order to build the national missile defense system--launching a terrifying new arms race.</P>
<P>For Bush and the arms merchants, the issue isn't whether missile defense really works. The issue is whether it can help Washington to bully the world.</P>


