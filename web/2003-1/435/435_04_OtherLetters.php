<p><a name="war"></a><font face="Times New Roman, Times, serif" size="4"><b>Students protest the war recruiters</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear Socialist Worker,</P>
</B><P>When we got the word that some military recruiters were coming to our school--Northwestern High School in Hyattsville, Md.--a group of friends got together the night before and made T-shirts and signs and came up with some chants.</P>
<P>On the morning they arrived, we started to chant in front of the school's main building. We were later joined by other students, and even some teachers.</P>
<P>As one teacher at Northwestern High wrote in an e-mail to local activists: "They were loud and boisterous and had very inventive slogans about the war in Iraq and the fact that Bush and his cronies won't be sending their children to die in the Gulf."</P>
<P>We followed our plan to protest in front of the Career Center where the military recruiters were supposed to go to that day. I and some other students formed a human chain in front of the Career Center door. We wouldn't move until the police came to get us. </P>
<P>In the end, they did not suspend us, and we are still allowed to protest. We are proud of this, and I hope to start a club soon.</P>
<B><P>Sheri Pegram, </B>Hyattsville, Md.</P>

