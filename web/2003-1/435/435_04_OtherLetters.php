<p><a name="lopsided"></a><font face="Times New Roman, Times, serif" size="4"><b>The U.S.'s lopsided priorities</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear Socialist Worker, </P>
</B><P>Now and then, I see bumper stickers that read, "America: Love it or Leave it." More often discontented with my citizenship than not, I must say that I am finding it difficult to find asylum in another country.</P>
<P>Why would I want to leave? America is one of the only first-world countries to have no socialized health care system and no free university system. Practically every aspect of an American's life is sold to them. It is difficult enough to walk through a store the size of a city block and realize that it was created solely to make a few people very rich. It's tragic to walk into a hospital and realize the same thing.</P>
<P>One of Bush's first acts as president was to push through legislation to provide a tax refund. Had I known that, two years later, there would be a huge budget deficit, I would have sent the check back. Now public schools are forced to cut programs, unemployment is no longer receiving funding, state parks are shutting down--the list goes on. Our invasion of Iraq is going to cost an estimated $3 trillion while elementary school teachers need to pay for many of their class supplies themselves. </P>
<P>When I think about this lack of perspective and hopelessly lopsided priorities, I wonder if political persecution can take the form of an assault on common sense. For now, I propose a new bumper sticker, "America: Difficult to Love, Difficult to Leave."</P>
<B><P>Joel Pedersen, </B>Tucson, Ariz.</P>

