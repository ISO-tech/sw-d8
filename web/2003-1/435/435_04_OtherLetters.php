<P><font face="Times New Roman, Times, serif" size="4"><b>Why Oregon human rights day is hollow</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear Socialist Worker,</P>
</B><P>In December, Oregon Gov. John Kitzhaber gave an official apology for the state's horrific eugenics law that forcibly sterilized over 2,600 people between 1900 and 1980. Some of these people were mentally challenged. Some were youths put in care by the state. Oregon is only the second state to offer such an apology out of 33 states that used similar eugenic laws to sterilize at least 60,000 people. </P>
<P>Kitzhaber also declared December 10 as human rights day--"a day on which we will affirm our commitment to the value of every human being in Oregon," he said. This comes a couple of weeks after two Portland cops were given an award for the killing of Mexican national, Jose Mejia Poot, one year earlier. His only "crime" was being an epileptic who spoke little English. The cops mistook him for being mentally ill. </P>
<P>Portland police already have a reputation for killing people with mental illness. And now, just days after Kitzhaber's "apology," the state has announced the eviction of 122 people with mental illness from residential care homes, in order to save money. They will likely be put into hotels, where they will not receive the care they need.</P>
<P>The most vulnerable citizens are again under attack. And for one day of the year, we are meant to think of their human rights? These are the symptoms of a sick group running society. Such events show why we desperately need a new kind of society where we take care of the weakest people, not shoot or sterilize them.</P>
<B><P>Paul Dean, </B>Portland, Ore.</P>

