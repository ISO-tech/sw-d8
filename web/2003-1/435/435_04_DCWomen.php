<P><font face="Times New Roman, Times, serif" size="4">"A tale of two cities" for women in the U.S. capital</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Left out in Washington</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 10, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear Socialist Worker,</P></B>
<P>With powerful women such as new House Minority Leader Nancy Pelosi (D-Calif.) and National Security Council Advisor Condoleezza Rice making headlines in Washington, D.C., some may have forgotten what life is really like for women who live here.</P>
<P>A recent study published by the Institute for Women's Policy Research (IWPR) showed that women in Washington, D.C., face the lowest gap between what they earn and what men earn compared to any other place in the U.S.</P>
<P>According to the IWPR report, women in D.C. earn 89.2 percent of what men earn, while nationwide, the gap is 72.7 percent.</P>
<P>The director of the study, Amy Caiazza, said that "there is a strong link between women's representation in political office and better policies for women." </P>
<P>Washington, D.C., is well-known for its fancy law offices and lobbying firms on Capital Hill. But far from being the norm, only a minority of women in D.C. possesses this type of wealth and power.</P>
<P>Working-class and poor women face jobs with low pay and few benefits, and they are among the most neglected when it comes to health care. Women here have some of the most severe problems associated with poverty, such as the highest rate of diabetes cases and the highest level of HIV infections nationwide.</P>
<P>Even the Institute's leaders were surprised by this. "It's like a tale of two cities," said Heidi Hartmann, director of the IWPR. "[I]n the District, there's high poverty and lack of health insurance, but there's also high pay for other women and the highest level of managerial and professional women."</P>
<P>Hartmann is right, but the attacks being waged on working-class and poor women in D.C. are far from a recent phenomenon, and are only likely to get worse. George W. Bush has ended unemployment benefits and is happy to cut even more social spending. But welfare programs were dismantled by the Clinton administration, in which Donna Shalala served as the Cabinet minister for Health and Human Services.</P>
<P>So much for Caiazza's theory. The study admits that the gap between women's wages and men's wages has been lessened not because women have made significant gains, but rather because of the lack of growth in wages for men.</P>
<P>In Washington, D.C., where a number of women hold political office and the city is represented by Democrat Eleanor Holmes Norton in Congress, two homeless shelters have been closed the only public hospital was shut down, and at least $30 million has been cut from the public school budget--all without a peep from any of our so-called "representatives in office," male or female. </P>
<P>Working-class people in D.C.--women and men together--have everything to gain from organizing along side one another for better wages, more social programs and access to health care.</P>
<B><P>Michele Bollinger, </B>Washington, D.C.</P>

