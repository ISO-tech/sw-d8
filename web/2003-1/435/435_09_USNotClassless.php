<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Why the U.S. isn't a classless society</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 10, 2003 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>U.S. CITIZENS have long been force-fed garbage about this country being a land where there are no class divisions, where everyone is "middle class." These ideas come under severe strain whenever economic crisis begins to stalk us.</P>
<P>It's hard to deny class when United Airline CEO Glenn Tilton, who received a $3 million signing bonus, a $4.5 million fully-funded pension trust and a $950,000 salary, slashes billions in wages from United employees. No doubt, if Tilton revives United through massive layoffs and wage and benefit cuts, he'll be rewarded with even higher compensation.</P>
<P>The companies that led the way in layoffs this year averaged compensation packages 80 percent higher than the CEOs of 365 companies surveyed by <I>Business Week</I>. Disney's Michael Eisner, for example, who earned $72.8 million in total compensation last year and whose company boasted a 33 percent increase in profits for the first quarter of 2001, laid off 4,000 employees in order to make the company "recession-proof."</P>
<P>The obscenity of capitalism was on display throughout the last decade (1990-2000):</P>
<P>--While executive pay jumped 571 percent, workers' pay increased only 37 percent (adjusted for inflation, only 5 percent). Today, executive pay is 531 times that of an average worker. The median wage (taking inflation into account) is still slightly below what it was in 1973.</P>
<P>--If the minimum wage had increased at the same rate as executive pay, it would be $25.50 per hour, not the miserable $5.15 that it actually is today--a wage that in real terms is lower than what it was in 1960. The average yearly pay of production workers would be $120,491 instead of $24,668. </P>
<P>--The Bush administration, in preparation for yet more tax cuts for the rich, has been arguing that the "tax burden" on the rich is too high. But between 1996 and 1998, according to one study, 41 large corporations used special tax breaks to reduce their tax load to--less than zero! The government doled out $3.2 billion in tax rebate checks to these companies.</P>
<P>--According to the latest census, the richest 20 percent make 13 times that of the poorest 20 percent. But these census figures exclude the richest 1 percent of the population--that is, everyone who makes over a million dollars. In 2001, 2.2 million people had assets, excluding real estate, of more than $1 million. Of these people, 150 are billionaires (there were 100 in 1992). The combined wealth of the top 10 billionaires is more than the gross domestic product of Mexico for an entire year. </P>
<P>--Meanwhile, the number of people living below the official poverty line jumped by 1.3 million in 2001, bringing the total up to 32.9 million people. To give you an idea of how much this figure underrepresents the number of poor people, a family of four with a yearly cash income of $18,102 is considered above the poverty line. A two-bedroom apartment in Chicago, by my rough estimate, costs at least $11,000 a year, leaving a family of four living just over the official poverty line with $7,102 for all other expenses.</P>
<P>These figures debunk the myth that everyone is well-off in the U.S. As if the 2,700 Verizon workers in the Northeast who were issued pre-Christmas pink slips--while the company's top nine executives received $336 million in compensation over the last five years--needed proof.</P>
<P>But the issue isn't just that some people make obscene amounts of money while others worry about how they are going to pay the rent. Class isn't just measured in income, but in who is producing and who is controlling society's vast wealth.</P>
<P>Quite simply, there is a minority that is idle, yet through their ownership of society's instruments of production gains all the wealth; and there is a majority that works, yet receives only a small fraction of the wealth it creates. And if you're anything like me, that makes your blood boil.</P>


