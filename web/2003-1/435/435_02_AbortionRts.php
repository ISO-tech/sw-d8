<P><font face="Times New Roman, Times, serif" size="4">Right to choose back at the top of Republican hit list</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Anti-abortion fanatics on the offensive</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 10, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ABORTION RIGHTS are back at the top of the Republican hit list. With the GOP in control of both the House and Senate, anti-choice politicians are gearing up when Congress convenes for a major assault on women's right to choose.</P>
<P>According to the <I>New York Times,</I> the first order of business will be yet another attempt to ban the late-term abortion procedure misnamed "partial-birth abortions" by the anti-abortion bigots. Counting the votes of conservative Democrats, Republicans now claim 60 senators are in favor of the ban--enough to get around a filibuster.</P>
<P>In addition, Republicans are reportedly planning to push for legislation that would make it a crime to take a minor across state lines for an abortion. They also want to allow hospitals to refuse to perform abortions without losing federal funding--and make it a separate crime to harm a fetus during an attack on a pregnant woman.</P>
<P>All of this amounts to the same thing--further whittling away women's already shrinking abortion rights. One reason for the new push is the election of Sen. Bill Frist (R-Tenn.) to replace Trent Lott as the Senate's new majority leader. Frist is an anti-choice zealot who over the past six years got a 100 percent rating on major votes from the bigots in the National Right to Life Committee.</P>
<P>"Few senators have a worse voting record&#133;than Trent Lott--but Bill Frist is one of them," said National Organization for Women (NOW) President Kim Gandy. "This is the man who is supposed to save face for the GOP in the Senate? Think again." </P>
<P>Gandy and Kate Michelman, head of the National Abortion and Reproductive Rights Action League (NARAL), both spoke out last week about the Republicans' looming attack on abortion. But one reason that the anti-choice bigots are so confident is that mainstream groups like NOW and NARAL have put their hopes in Democrats.</P>
<P>From the late 1970s, when Democratic President Jimmy Carter championed limits on abortion access for poor women, to the eight years of Bill Clinton and Al Gore, who ignored further restrictions on abortion across the country, the Democrats have shown that they can't be trusted to protect abortion rights.</P>
<P>Last week, NARAL announced it would spend millions of dollars on a new public relations campaign to counter the anti-choice onslaught--as well as challenge any new anti-choice laws in the courts. But we have to remember how abortion rights were won in the first place--by ordinary people, women and men, organizing at the grassroots to put pressure on the politicians.</P>


