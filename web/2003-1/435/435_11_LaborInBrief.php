<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 10, 2003 | Page 11</font></p>

<P><font face="Times New Roman, Times, serif" size="4"><b>Graphic Packaging</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>KALAMAZOO, Mich.--The union representing 400 locked-out workers at Graphic Packaging Corp. has launched a boycott of the company. Graphic's corporate greed was exposed in November when a temporary worker hired to replace locked-out union members was crushed to death at the mill here.</P>
<P>Graphic locked out more than 400 members of Paper, Allied-Industrial, Chemical and Energy Workers International Union (PACE) Local 6-1010 on July 27. "They came in their paramilitary style boots and just told everyone to gather up their belongings and leave the premises," is how Robin DeHollander, who has worked more than 26 years at Graphics, described it to <I>Socialist Worker. </I>"They had the police department with dogs in the cars in the parking lot. I've never been through anything like it. You figure that's your whole life."</P>
<P>Management wanted workers to agree to 20 hours of additional mandatory overtime as well as work more weekends and holidays. Graphic also wanted more restrictive attendance policy, in which doctors' excuses weren't accepted except in the case of radiation, chemotherapy or dialysis. The company also demanded a cap on new hires' pensions at $25 per year of service, compared to the current $31 per year, as well as a limit on new workers' vacation time.</P>
<P>But Graphic bosses aren't the only ones attacking Local 6-1010. The state of Michigan has denied workers unemployment benefits. Union members have been harassed by police on the picket line--and 100 of them were denied applications when they tried to apply for work at a local temp agency.</P>
<P>Many workers and their families have been forced to rely on charitable organizations to supplement the $60 a week they receive from the union, and the local has set up a food bank.</P>
<P>Colorado-based Graphic is controlled by the notoriously anti-union Coors' family, which was the target of a nationwide AFL-CIO boycott 1980s. At a January 2 press conference, PACE launched a national boycott against Graphic, backed by the AFL-CIO and supported by United Students Against Sweatshops. The initial focus is Coor's brewery, but food companies General Mills, Quaker Oats and Kraft will also be targeted.</P>
<P>Building community support has been crucial to this struggle. "At first the community didn't understand and we ran an ad in the local gazette explaining what we were asking for," said DeHollander. "We work a lot of overtime. It's a moral issue--everybody needs to have a life outside of work. And they were trying to take that away from us. We faced an extremely tough time at Thanksgiving and Christmas. Other unions and churches really banded together, bought presents for our children, donated turkeys and food. A lot of people understand that this is a fight for everybody, it's not just us."</P>
<I><P>Donations can be made with checks or money orders payable to Local 6-1010 Lockout Relief Fund, and mailed to PACE Local 6-1010, 716 Shoppers Lane, Parchment, MI 49004. For more information on the boycott, visit www.coors-abuse.org on the Web.</P></I>

<P><font face="Times New Roman, Times, serif" size="4"><b>Domino Sugar</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Rob Savidge and Alex Billet</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>BALTIMORE--"It wasn't the Grinch who stole Christmas, it was Domino." So said 330 Domino Sugar workers this December, when the company forced them to strike. Ever since management walked out of negotiations on December 8 without even waiting for the union's proposal, workers have been walking the picket line 24/7. </P>
<P>Management demanded the right to change anything not specifically laid out in the contract without any consultation with the union. Also, management wants to shift the workers' retirement funds to a company-run pension--without disclosing details. </P>
<P>"We won't let Domino destroy our retirement plans like Enron and WorldCom," said Alex Hamilton, a 32-year sugar worker and president of United Food and Commercial Workers Local 392. "Workers deserve a secure pension, not an insecure future."</P>
<P>Domino also wants to cut some holidays--including Veteran's Day, despite the fact that many workers are themselves veterans. They also want to increase the cost of family health insurance, lower the quality of that insurance and reduce wages and benefits for all new hires.</P>
<P>With support and solidarity from the rest of the labor movement, the workers plan to fight until they get a contract to their liking.</P>

