<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">White House economic plan:</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"Stimulating" more money for the rich</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 10, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WHO DO they think they're kidding? That's the only possible reaction to the economic "stimulus" package that George W. Bush will propose this week. The main thing that it's designed to stimulate is the swollen bank accounts of the already superrich.</P>
<P>Oh, you'll hear about money for state governments suffering record budget deficits. And Bush may even shed a crocodile tear or two as he promises to urge Congress to extend the federal government's emergency program of supplemental unemployment benefits. The, uh&#133;well, yes, the very same program that he allowed Republican congressional leaders to kill in December.</P>
<P>But all that is the PR varnish to cover up the guts of the Bush stimulus scam--a vast array of new tax breaks for the country's richest people. The White House wants to speed up the $1.35 trillion tax cut handout passed in 2001, which heaps 43 percent of its benefits on the richest 1 percent of taxpayers--and take another shot at eliminating the estate tax, which is paid only by the richest 2 percent of the population.</P>
<P>By comparison, the all-new proposal for slashing taxes on stock dividend income is positively democratic--the top 1 percent will rake in only 25 percent of the benefits of this $150 billion-plus giveaway.</P>
<P>The administration doesn't mind admitting that they want to transfer money to the rich. This, they claim, will stimulate the economy--by putting cash in the hands of people who will invest it. But the record of this "supply-side economics" swindle--once preached by Ronald Reagan and dismissed by George Bush Sr. as "voodoo economics"--is that the benefits which are supposed to "trickle down" never do.</P>
<P>Even before announcing the details, Bush was pre-emptively denouncing anyone who might disagree with him for "[turning] this into class warfare." But is there any better description of what the Bush gang is doing? This is Robin Hood in reverse--stealing from the poor to give to the rich.</P>
<P>As if to underline how little it cares about the suffering caused by the economic slump, the White House last month quietly killed off a Labor Department program that tracked mass layoffs by U.S. companies. </P>
<P>This monthly statistic showed just how hard job cuts are hitting--recording nearly 17,799 mass layoffs (defined as 50 workers or more) involving nearly 2 million people, from January to November of last year, when the economy was supposed to be out of recession. But the Bush team says that it can't see, in the words of Labor Department hack Mason Bishop, "what value this program has."</P>
<P>As details of the Bush stimulus plan leaked out to the press, Democrats in Congress came to life--sort of. House Minority Leader Nancy Pelosi (D-Calif.), for example, ripped the White House proposal as "too little, too late, but we'll just see how little it is."</P>
<P>But when it came time to offer an alternative, Pelosi and the Democrats sidestepped measures that would provide real relief to working people--like a cut in the regressive payroll tax that hits harder the smaller your paycheck.</P>
<P>A payroll tax holiday, Pelosi said, would drain money from the Social Security trust fund. In other words, the Democrats are still trying to out-Republican the Republicans as the most fiscally responsible party.</P>
<P>No one in the Washington establishment is talking about restoring spending on desperately needed programs to help poor and working families survive the economic crisis--only about how big the tax cut bonanza for the rich will be.</P>


