<P><font face="Arial, Helvetica, sans-serif" size="2"><b>ISSUES IN THE LABOR MOVEMENT</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Why labor should oppose war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Donny Schraffenberger, shop steward, Teamsters Local 705</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 10, 2003 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ALTHOUGH MANY labor leaders argue that U.S. foreign policy is of no concern to union members, some labor leaders and rank-and-file members are now proving them wrong.</P>
<P>My local, Teamsters 705, passed an anti-war resolution at a general membership meeting in September by an almost unanimous voice vote, with one lone dissenter. Teamsters, many who are Vietnam War veterans, went to the open mic and told of their harrowing experiences in war--and why they don't agree with George W. Bush that a war against Iraq is necessary. Only one speaker argued for war.</P>
<P>The meeting showed how war affects everyone who works for a living. The priorities of the Bush administration are to channel money into the military machine, while cutting social programs that benefit the working class. And many in Local 705 are being hurt by the sluggish economy and are angry at CEOs who've lined their pockets at our expense.</P>
<P>Workers in this country don't benefit from the U.S.'s orgy of conquest and plunder. War, instead of preserving our "freedoms" from outside "aggressors," actually is used to justify attacks on our rights at home. The Homeland Security Act is the most recent example.</P>
<P>During wartime, our bosses and rulers try to curtail our liberties. During the First World War--the war to make "the world safe for democracy"--anti-war activist and prominent socialist Eugene Debs was thrown in prison for publicly speaking against the horror of an imperialist war. After the war, the FBI rounded up immigrants and anyone considered a "red."</P>
<P>Instead of the war expanding democracy, the ruling class used its expanded repressive authority as a sledgehammer to smash the workers' movement after the war was over.</P>
<P>During the two World Wars, the bosses wanted "peace" with their workers, because profits were so high. Some of the fat cats would even tolerate unionization while they were making out like bandits. But as soon as the "good times" of the bloody war profits were over, class war with a vengeance was opened up on working men and women.</P>
<P>The bosses wanted a no-strike pledge in the Second World War that many of the AFL and CIO leaders were willing to give. Yet, even after the war was over, many of the CEOs wanted to extend the no-strike pledge forever.</P>
<P>Our government always accuses its enemies of spying on their own populations, but the U.S. knows a thing or two about spying on its own people. The FBI spied on antiwar and civil rights activists during the Vietnam War era.</P>
<P>Also, it's a myth that war creates prosperity. It's true that during both world wars the economy skyrocketed. But it's also true that tens of millions of people were brutally killed and maimed--more than a small price to pay. Yet, the Vietnam War didn't bring prosperity to the U.S. economy. On the contrary, war spending helped make the economy more unstable.</P>
<P>The "war on terrorism" and the new war set for Iraq have already ratcheted up government oppression of Arabs, immigrants, antiwar protesters and unionized workers.</P>
<P>And rather than create jobs, Corporate America, with the blessing of the Bush administration, is using the situation to go after workers--for example, locking out the West Coast dockworkers, imposing layoffs at Verizon and trying to cancel union contracts at United Airlines.</P>
<P>There will be no economic miracle for American workers trickling down from this series of never-ending wars. Instead, we are the ones that will have to pay for these wars in lower wages and reduced benefits, gutted social services, and--for some of us--with our lives.</P>
<P>That's why union members and organized labor has to stand up now to this coming war and the so-called "war on terrorism."</P>


