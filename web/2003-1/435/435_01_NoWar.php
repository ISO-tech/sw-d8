<font face="Times New Roman, Times, serif" size="5"><b>No to Bush's war machine!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 10, 2003 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WHAT NERVE! Asked about his New Year's resolutions by a reporter as he strutted around his Crawford, Texas, ranch last week, George W. Bush declared: "This government will continue to lead the world toward more peace. And we hope to resolve all the situations in which we find ourselves in a peaceful way."</P>
<P>Sure. That's why the Pentagon doubled its deployment of troops in the Persian Gulf over the holidays. That's why military officials are honing plans for an invasion of Iraq--to be preceded by an air war in which U.S. warplanes fly some 500 to 1,000 bombing runs on the first day alone, according to press reports.</P>
<P>Humanitarian aid groups in and around Iraq predict that a war will almost certainly cause a refugee crisis. They warn of outbreaks of typhoid and cholera on an "epidemic scale" if the U.S. bombs Iraq's power stations, crippling the water treatment system--as it did during the 1991 Gulf War. Christian Aid estimates that once the air war begins, stores of food throughout the country will run short in just two weeks.</P>
<P>War. Disease. Starvation. That's what Washington has in store for Iraq.</P>
<P>A week later, Bush was peddling his war to soldiers at Camp Hood--and claiming that it was "humanitarian." The U.S. is going "not to conquer, but to liberate," Bush claimed.</P>
<P>But the Pentagon war plan leaked earlier this week looks like conquest. It anticipates a U.S. military occupation of at least 18 months--and, of course, the seizure of Iraq's oilfields. And after that, a country ruled by the gangsters and CIA stooges who make up the Iraqi "opposition."</P>
<P>After searching for weeks, United Nations inspectors have yet to find proof of "weapons of mass destruction" in Iraq. "If we were to publish a report now," one inspector admitted to the <I>Los Angeles Times, </I>"we would have zilch to put in it."</P>
<P>That underscores what Bush's war drive against Iraq is really about--not searching for weapons of mass destruction, but using them. Washington's goal isn't just "regime change" in Iraq or even control of Middle East oil. It's to rewrite the rules of world politics--and make the U.S. more powerful than ever.</P>
<P>This relentless drive to war has led more and more people to question Bush's rhetoric about "peace" and "liberation"--and turn out for campus teach-ins and local protests to show their opposition. </P>
<P>Naturally, the corporate media has given a platform to a few liberal commentators who do their best to cast doubt not on Bush's war drive, but on the antiwar movement. In a <I>New York Times Magazine</I> article titled "The Liberal Quandary Over Iraq," author George Packer complains that an antiwar movement "controlled by the furthest reaches of the American left" has put forward "unnuanced" slogans, like "No Blood for Oil" and "No Sanctions, No Bombing." As if activists who want to stop a one-sided slaughter in Iraq--or end the sanctions that have killed more than 1 million Iraqis in the last decade--have something to apologize for!</P>
<P>The truth is that millions of people doubt Bush's case for war--and organized opposition is growing. More than 200,000 people protested the war on October 26 in Washington, D.C., San Francisco and other cities. Activists are building for follow-up protests on January 18, where we'll send a message that Bush's war makers and their apologists can't ignore.</P>
<P>Bush may be determined to have his war--but a growing number of people are just as determined to stop him.</P>


