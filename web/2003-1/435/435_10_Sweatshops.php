<P><font face="Times New Roman, Times, serif" size="5"><b>Stop sweatshop labor</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Joe Cleffie</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 10, 2003 | Page 10</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Last month, more than 500 protesters gathered for a candlelight vigil in the rain at Niketown to protest the use of sweatshop labor.</P>
<P>The annual march--sponsored by groups like UNITE, Jobs with Justice, New York Public Interest Research Group (NYPIRG) and many others--demanded a living wage for sweatshop workers at corporations like Nike, Disney, Kohl's and others. </P>
<P>Workers, for example, who make Minnie Mouse toys for Disney in China and Indonesia often work up to 15 hours each day and make 20 to 25 cents per hour. And when they try to organize for better conditions, they're often harassed, fired or assaulted. </P>
<P>"We are trying to expose these companies' brutal labor practices, to stand in solidarity with workers overseas and to help workers here in the U.S. understand that sweatshop conditions for any workers are an attack on the standard of living for all of us," explained a NYPIRG organizer.</P>
<P>Signs on the march--and chants of the old labor slogan "An injury to one is an injury to all"--summed up this idea.</P>


