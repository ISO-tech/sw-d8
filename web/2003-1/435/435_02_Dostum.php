<P><font face="Times New Roman, Times, serif" size="4">UN accuses Afghan warlord</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>When the U.S. loves a torturer</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 10, 2003 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>A U.S.-BACKED warlord of the Northern Alliance is being investigated by the United Nations (UN) for ordering several witnesses to his war crimes to be jailed and tortured. Gen. Abdul Rashid Dostum has been implicated in the deaths of up to 1,000 Taliban prisoners last year.</P>
<P>According to witnesses, soldiers surrendering to Dostum's forces after the battle for the city of Kunduz were blindfolded, sometimes handcuffed and then beaten--before being crammed into metal shipping containers with no air holes and no water. The containers were then driven by dozens of trucks across the desert to Sheberghan prison. No one knows how many died along the way. Now, the UN is confirming that several witnesses to the atrocity were jailed to stop them from testifying.</P>
<P>U.S. Special Forces troops worked closely with Dostum during the war--and following the Taliban's overthrow, he was "rewarded" with the post of deputy defense minister in the new U.S.-backed government.</P>
<P>But backing this butcher was a "necessary move," according to the <I>New York Times</I>. "[I]t followed the same logic that led American forces, in the early days of the Afghan war, to cooperate with--and, according to recent revelations, pay large sums of money to--some decidedly unsavory characters: better they be allies than enemies," the <I>Times</I> wrote.</P>


