<P><font face="Arial, Helvetica, sans-serif" size="2"><b>SAY NO TO BUSH'S WAR ON IRAQ</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Tens of thousands involved in March 5 protests</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Students take action</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 14, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"WE'LL PROBABLY get a Saturday detention and maybe lose some class credit," said Alec Losh, a student at Warrenville South High School near Chicago. "But if this war happens, a lot of people are going to lose their lives. There's no contest between the two." </P>
<P>Losh was among the tens of thousands of students who took part in local actions in hundreds of cities and schools across the country March 5 to protest Bush's war on Iraq.</P>
<P>In Evanston, Ill., 1,000 students from Evanston Township High School--about a third of the student body--walked out and marched through the downtown, shutting down traffic at midday. About 600 students at the University of Illinois-Chicago came to a teach-in with speakers that included Rev. Jesse Jackson--and followed it up with speak-out in the cafeteria. Later in the day, 3,000 protesters from all over Chicago joined forces for a rally and march downtown.</P>
<P>This was repeated across the country. Thousands marched in New York, rallying at pro-war Sen. Hillary Rodham Clinton's office--then heading to Washington Square Park. "This is supposed to be a democracy, but when the politicians aren't listening to us, we have no choice but to take to the streets," said Columbia University student Minou Armojand.</P>
<P>During the course of the march, four people were arrested--further underscoring the gross hypocrisy of the Bush administration's claim that it wants to go to war to bring "democracy" to Iraq. </P>
<P>New York University antiwar activists used March 5 to demand that the school open its books and divulge any investments it has in companies that profit from war. At Hunter College, 700 students walked out of class after teams of activists chanted in hallways and classrooms, encouraging others to join them.</P>
<P>In Madison, Wis., 3,000 students--half of them from high schools--walked out of classes and marched to the State Capitol and shut it down for several hours. At the University of Maryland outside of Washington, D.C., as many as 1,000 students, staff and faculty took part in a student strike against the war.</P>
<P>The strike was organized by several groups, including the Maryland Peace Forum, the ISO, Students and Workers Unite and the Muslim Students Association, and was endorsed by 35 other student organizations, 150 professors and AFSCME Local 1074.</P>
<P>About 700 people participated in a march through campus called by the Stop the War Coalition at the University of California-Berkeley. At Stanford University, 1,000 people turned out to an exciting daylong teach-in. "I've never seen anything like this," said Meghana Reddy. "It's incredible--it wasn't the ordinary crowd."</P>
<P>At the University of California-Santa Cruz, 300 students and faculty came out for a walkout and rally. Some 500 students from San Francisco State University participated in a lively rally in Malcolm X Plaza. Afterwards, about 200 students blocked traffic on 19th Avenue for more than an hour.</P>
<P>In San Diego, hundreds of high school and college students participated in events--rallies, marches, walk-outs and teach-ins--as part of the Books Not Bombs campaign. At the University of Vermont, students organized "alternative classes," which included topics like "Beyond Iraq: War on Colombia," and two rallies on March 5.</P>
<P>A teach-in at the University of Massachusetts-Amherst featuring Anthony Arnove and Howard Zinn and sponsored by the <I>International Socialist Review</I> drew some 250 people.</P>

<B><P>International Women's Day</P></B>
<P>On March 8, International Women's Day, some 5,000 people took to the streets of Washington, D.C., to protest the war. The march was organized by Code Pink.</P>
<P>After waiting all afternoon, protesters heard from the National Park Police that they had been denied their right to march to the White House. Dozens of protesters, including author Alice Walker, were arrested trying to cross the police line. As one protester put it, "Why would you deliberately impede the exercise of constitutional rights by a group of grandmothers wearing pink? What are they afraid of?"</P>
