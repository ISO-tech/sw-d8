<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Can a stronger UN stop the war?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 14, 2003 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ON MARCH 6, George W. Bush called on the United Nations (UN) Security Council to endorse his war plans and, in the next breath, said if it didn't, the U.S. would go to war anyway.</P>
<P>It's conceivable that Washington's threats and bribes can still win enough support on the Council to claim a nine-vote majority for war even if France or Russia vetoes it. So if the U.S. goes to war without its approval, will this make the UN "irrelevant," as Bush consistently warns?</P>
<P>Answering this question requires an understanding of the fact that the UN represented the political component of the allied powers' settlement to the Second World War. It was the vehicle by which the U.S. believed it could run the world in Washington's interests.</P>
<P>For that reason, the U.S. insisted that Security Council "permanent members" should have a veto over any resolution. "We should not forget that this veto power is chiefly for the benefit of the United States," Secretary of State Cordell Hull told leading senators in 1943.</P>
<P>In addition, the U.S. and Britain pushed for the inclusion of France and China (then under the anticommunist puppet Chiang Kai Shek) as permanent members, even though they hardly qualified as world powers in 1945. In this way, the U.S. and U.K. hoped to guarantee that "the West" held a 4-to-1 majority over the Soviet Union among the veto wielders on the Security Council.</P>
<P>But as the Cold War gathered steam after 1947, the Security Council became hopelessly divided between the USSR and the West. Since almost every issue over the next 40 years split along U.S./USSR lines, most didn't even reach the Security Council because they would have triggered an automatic veto.</P>
<P>Only with the fall of the Soviet Union and the emergence of a single-superpower world did the U.S. feel that the Security Council was "relevant" to its plans again. That's why from 1945 to 1990, the Council passed only 650 resolutions, compared to more than 800 resolutions since then. Yet when the U.S. saw that the Security Council wouldn't bend to its will, it ignored the UN--as when it avoided a Russian veto by using NATO to carry out the 1999 war over Kosovo.</P>
<P>In taking its Iraq war plans to the UN, the U.S. thought it could win "international" backing and it could camouflage its real aims in the rhetoric of disarmament. But so far, the U.S. gambit hasn't worked. Other ruling classes represented on the Security Council decided that their national interests don't coincide with those of the U.S. They see the war in light of the Bush Doctrine's proclamation of a U.S. drive for unchallenged global domination.</P>
<P>France, Russia and China don't have the economic or military might to compete with the U.S. Still, their presence on the Security Council--a legacy of 1945--allows them an opportunity to at least try to slow down U.S. war plans. If one of them exercises a veto, it won't stop the U.S. war. But it will expose the UN as a hollow shell.</P>
<P>Likewise, the open bullying the U.S. is using to win support shows how little UN decisions have to do with the high-sounding rhetoric about disarmament and the will of the international community. Many in the antiwar movement might see this as a reason to call for a reformed UN that expresses the will of the world's people. But this won't happen.</P>
<P>First, the UN represents the governments--and therefore, the ruling classes--of its members. Pro-war governments like Britain and Spain are defying the overwhelming antiwar sentiment of their populations.</P>
<P>Second, an international organization like the UN won't escape the underlying reality of U.S. superpower domination of world politics--just as the UN couldn't escape the straightjacket the Cold War imposed on it.</P>
<P>As long as the world system is composed of competing nation states in a world capitalist economy, any international organization will simply act as the backroom where thieves gather to divide up their spoils.</P>


