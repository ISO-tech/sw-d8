<P><font face="Times New Roman, Times, serif" size="4">Arrested at the mall...for wearing an antiwar T-shirt</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"I wanted to show I was for peace"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 14, 2003 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>STEPHEN DOWNS became internationally known last week for taking a stand at an Albany, N.Y., shopping mall. Downs and his son Roger wore antiwar T-shirts at the Crossgates Mall, and when security guards ordered Stephen to take off the shirt--bearing the words "Peace on Earth" and "Give Peace a Chance"--he refused and was charged with trespassing. Within days, hundreds of antiwar opponents had descended on the mall to protest the absurd arrest--each wearing antiwar attire.</P>
<P>Here, Stephen talks to <I>Socialist Worker</I>'s Ashley Smith about his action.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>I'VE BEEN frustrated at the inability to be heard. Just like other people, I have written letters to Congress. But there's no response. We are running out of ways to make our voices heard. </P>
<P>That's when I got the idea of the T-shirt in the mall. I had heard that people had walked around the mall in peace T-shirts, and some were confronted by security guards and told to take them off or leave the building. I was very troubled about what had happened to these people. So I thought what would happen if I did this? </P>
<P>I wanted to say something as a 60-year-old grandfather, so that my kids and grandkids would know that I was for peace. </P>
<P>So I went to a store that made t-shirts and ordered a couple with a peace slogan, and 15 minutes later, my son and I put them on and walked around the mall shopping. Most of the response to my shirt was positive. People said: "Love your shirt," and asked, "Where can I get one?"</P>
<P>The mall has said that it got complaints from a customer in Macy's. But we were never even near Macy's. The pro-war people have learned that they can go up to the security guards, complain about peace people and ask to have them removed. It's almost like they're on a campaign to get rid of peace people. </P>
<P>They're keeping us in fear, and fear is always bad when you need to make a big decision. To make a clear decision, you need freedom of speech--freedom to say what you think. What happened to me is a good example. </P>
<P>They're so afraid of the consequences of honest speech that they would rather suppress it. But the mall can't be declared off-limits to free speech. The First Amendment trumps any restrictions they might place on people in the mall. Now they would like to maintain a Disney World fantasy in the mall, so that nothing would interrupt shopping, but that's just not realistic. The mall is a public gathering space. </P>
<P>I was arrested by a corporation bent on self-destruction. There is a lot I love about America and its Constitution. If we lose our right to free speech in this way, we've lost the whole thing. </P>
<P>What I did was simple. What's significant is not what I did, but the reaction people have had to it. I've had at least 20 interviews a day from the news media from all over the world since this happened. I was downloading my e-mails yesterday. I had received 200 e-mails just yesterday, and 99 percent of them were supportive. </P>
<P>They were beautiful. I got all teary when I read them. People said touching things, affirming the things in the Constitution that are important, and they were outraged about what the hell's going on in this country. </P>
<P>An Iranian family that lives next door to us was amazed. They said, "What a country. You got arrested, and now you're a hero." </P>
<P>That got me thinking about the immigrants who are targeted right now. I'm a 60-year-old white guy, but what's happening to them is far more serious. What would have happened if the person wearing the T-shirt was a 30-year-old Palestinian? &#9;We may need to set up an underground railroad of safe houses to help them. If anyone does set up an underground railroad, I'll volunteer to provide a safe house. </P>
<P>Free speech should apply to everyone and should protect all speech, and not just the pabulum I put on my T-shirt. </P>


