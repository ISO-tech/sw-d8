<P><font face="Times New Roman, Times, serif" size="4">Ludicrous story line makes <I>David Gale</I>&#133;</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>An insult to abolitionists</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Helen Redmond</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 14, 2003 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>MOVIES: <I>The Life of David Gale,</B></I> directed by Alan Parker, starring Kevin Spacey, Kate Winslet and Laura Linney.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>NOW SHOULD be the best time ever for a Hollywood drama that exposes the barbarity of the death penalty and makes the case for abolition. In the last few years, support for the death penalty has declined, as the flaws in the system--sleeping lawyers, prosecutorial misconduct, racism--have been revealed.</P>
<P>More then 100 death row inmates have been exonerated and state moratoriums have been won, giving confidence to a growing movement of death penalty opponents. But you'd never know this to watch <I>The Life of David Gale</I>.</P>
<P>This movie, like others that Hollywood has made that claim to address a controversial social issue, dodges most of the controversy and instead relies on cookie-cutter formulas from genres like the "thriller."</P>
<I><P>David Gale</I> is set in Texas, the execution capital of the U.S. Kevin Spacey plays Dr. David Gale, a philosophy professor and a leading abolitionist in the group Deathwatch. His best friend, Constance (Laura Linney), is also a leading abolitionist.</P>
<P>So far so good--a film that's set in the murderous state of Texas centered around an activist organization implacably opposed to the death penalty. But then, the plot takes an utterly preposterous turn.</P>
<P>Using flashbacks, we learn that Gale has been given the death penalty for murdering Constance. Incredibly, the audience is asked to believe that a middle-class white man, who leads a high-profile, anti-death penalty organization, is getting a death sentence. This would be laughable if it weren't so insulting--considering that it's almost always poor and Black people who get the death penalty in Texas.</P>
<P>Gale asks to meet with a journalist, played by Kate Winslet. The plot then takes another twist--or should I say dive. The film builds up to the last 20 minutes, in which the audience learns why and how Constance died. Readers beware: I'm about to give away the ending.</P>
<P>Constance and David mistakenly and stupidly believe that, if a prisoner is exonerated, it shows that the death penalty system really works--that it protects innocent people from being mistakenly put to death. They conclude that if they could only prove that innocent people are put to death, they could win a moratorium or abolition.</P>
<P>Here is where the film loses every shred of credibility. Constance, who is terminally ill with leukemia, decides to commit suicide, make it look like a murder and "frame" David, who has decided to martyr himself for the cause. The film's "message" is that the abolitionist movement needs people sacrificing their lives in order to expose the flaws of the death penalty.</P>
<P>This is not only an absurd notion, but it's a slap in the face to death row inmates, their families and death penalty activists who have won significant gains without resorting to death pacts. This movie should be retitled "The Pathetic and Wasted Life of David Gale" and boycotted.</P>


