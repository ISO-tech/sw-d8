<P><font face="Times New Roman, Times, serif" size="4">Justices okay cruel law</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The Supreme Court's three strikes crime</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 14, 2003 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>STEAL THREE golf clubs. Go to prison for life. That's the reality of the California "three strikes and you're out" sentencing law. And last week, the U.S. Supreme Court upheld this sick law in a 5-to-4 decision.</P>
<P>The law, passed in a 1994 referendum after a 12-year-old girl named Polly Klass was murdered by a felon on parole, calls for a sentence of 25 years to life for all third felony convictions--even if the crime wasn't violent, and even if the crime is a misdemeanor that prosecutors choose to charge as a felony.</P>
<P>The law is so harsh that even Mark Klass, Polly's father, has called for its repeal. But the Supreme Court's right-wing majority decided last week that it wasn't "cruel and unusual punishment" for a man named Gary Ewing to receive a sentence of 25 years to life--after being convicted of shoplifting three golf clubs.</P>
<P>According to the Court, the law is simply part of the state's right to fight crime. "To be sure, Ewing's sentence is a long one," wrote Justice Sandra Day O'Connor for the majority.  "But it reflects a rational legislative judgment, entitled to deference, that offenders who have committed serious or violent felonies and who continue to commit felonies must be incapacitated."</P>
<P>Rational? Gary Ewing could spend the rest of his life in prison for stealing three golf clubs. In fact, he'll receive a longer prison sentence than people found guilty of murder or other violent offenses, and his prison term is two to three times longer than under "three strikes" laws in other states.</P>
<P>The Court also upheld the conviction of Leandro Andrade under the same "three strikes" law. Andrade, a heroin addict, was sentenced to 50 years to life for shoplifting two videotapes. He was 37 years old at the time he was convicted. And O'Connor had the gall to comment that Andrade--who will be 87 years old before he's released--"retains the possibility of parole."</P>


