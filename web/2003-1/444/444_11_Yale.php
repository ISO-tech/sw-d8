<P><font face="Times New Roman, Times, serif" size="5"><b>"Yale is a scene of solidarity"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Naveen Jaganathan</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 14, 2003 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW HAVEN, Conn.--More than 3,000 Yale workers walked off the job last week, demanding fair contracts and union recognition. The strike--by members of Hotel Employees and Restaurant Employees (HERE) Locals 34 and 35, the graduate student union GESO and Service Employees International Union District 1199 hospital workers--transformed Yale, known for its Ivory Tower snootiness, into a scene of solidarity and activism. </P>
<P>Graduate students joined hands with cafeteria workers to take part in spirited picket lines and sing "Solidarity Forever." The week was filled with rallies, speak-outs and education on the streets. A couple of thousand people took part in a march and rally featuring Rev. Jesse Jackson on March 3, followed by an even bigger rally two days later in the biting cold and snow which featured Princeton University professor and activist Cornel West and AFL-CIO President John Sweeney. </P>
<P>Jackson set the tone on the first day of strike, mobilizing a march through the New Haven community, where supporters came out of their houses to cheer on the unions. New Haven high school students, who are demanding that Yale provide scholarships and funds to improve the community schools, also participated in the march. </P>
<P>"It's disgusting that Yale is sitting on a $10.5 billion endowment while much of New Haven has to live in poverty," one student told <I>Socialist Worker</I>. "It's time Yale gave back to New Haven." Cornel West gave a fiery speech, saying the strike was historic in many ways. Jackson blasted George W. Bush, saying that "his administration is one of the worst anti-worker, anti-woman, anti-minority, anti-union administrations in history."</P>
<P>Keisha Williams, a member of HERE Local 34, told <I>Socialist Worker</I> that at first she didn't understand why Jackson was talking about his stance on war and other issues, "[but] then I realized what is happening to us at Yale is what is happening across the country." </P>
<P>Undergraduates organized a workshop on "Labor and War," which was attended by many GESO workers. They also organized "Education in the Streets," to which some 500 undergraduates came out to learn about the unions' struggle at Yale.</P>
<P>Compared to past rallies, the job action set a new tone. The large turnout of union membership to take part in the strike action has given confidence to Yale workers that they can win. "There are more scientists on the picket line now at the end of the week than there were at the beginning, because people know we're winning," said GESO member Maris Zivarts. </P>
<P>As <I>Socialist Worker</I> went to press, workers ended their five-day strike as Yale students went on spring break and were debating whether to continue job actions when school resumes. Yale unions should keep up the pressure on the university--and build on this momentum to win all their demands! </P>


