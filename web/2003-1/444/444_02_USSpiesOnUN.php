<P><font face="Times New Roman, Times, serif" size="4">U.S. dirty tricks exposed</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Spying on the Security Council</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 14, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WHEN IT comes to getting its way, the U.S. will stop at nothing. To gain maximum advantage as the United Nations (UN) deliberates a resolution authorizing a war on Iraq, the U.S. National Security Agency (NSA) issued a memo directing senior agents to spy on UN Security Council members. "The Agency is mounting a surge particularly directed at the UN Security Council members (minus U.S. and GBR [Great Britain] of course) for insights," reads the memo.</P>
<P>That meant tapping home and work phones and intercepting e-mail of Security Council diplomats to learn of "plans to vote on any related resolutions, what related policies/negotiating positions they may be considering, alliances/dependencies"--"the whole gamut of information that could give U.S. policymakers an edge in obtaining results favorable to U.S. goals." The memo--which was leaked to Britain's <I>Observer</I> newspaper--was sent by Frank Koza, chief of staff in the NSA's Regional Targets section.</P>
<P>Spying on diplomatic delegations is a direct violation of international law--not that the U.S. government cares. The revelation caused a political furor in Chile, and Chilean President Ricardo Lagos demanded an immediate explanation.</P>
<P>But the U.S. corporate media has generally ignored the story entirely, or at best downplayed its significance--by quoting unnamed intelligence officials whose justification is that this sort of spying happens all the time.</P>
<P>"It would be inconceivable to me, with the interest of the nation's leadership on this set of issues, that we aren't using all available means to collect as much information as possible," according to a former intelligence official quoted in the <I>Los Angeles Times</I>. </P>


