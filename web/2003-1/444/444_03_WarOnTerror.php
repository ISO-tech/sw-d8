<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The truth about the "war on terror"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 14, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>TWO WEEKS ago, the media announced amid much fanfare the arrest of Khalid Sheikh Mohammed--Osama bin Laden's supposed "number three man" in the al-Qaeda network. Never mind that almost no one had ever heard of this "terrorist mastermind" before.</P>
<P>The administration used Mohammed's capture to hit back at Democratic Party critics in Congress, who claim that the White House is neglecting the pursuit of the "evildoers" responsible for September 11 while pushing its war on Iraq. The Bush gang also hopes that, by again raising the horror of September 11, they can get more backing for their "war on terrorism"--next stop Iraq.</P>
<P>But if George W. Bush wants to get to the heart of the terrorist threat, he should follow the trail left by the U.S. government itself. After all, it was the administration of Bush's hero, Ronald Reagan, which backed the hard-line Islamist mujahadeen during Washington's covert war against the former USSR's occupation of Afghanistan in the 1980s. The CIA helped to train the likes of Osama bin Laden, who was counted among the "freedom fighters" of Afghanistan, in the words of Reagan himself.</P>
<P>When the USSR was chased out of Afghanistan in 1989, Washington's use for the Islamists came to end--and Bush's father washed his hands of the place, leaving it to local warlords to battle over the remains of the country and thereby stoking hatred of the U.S. among the abandoned Islamists.</P>
<P>Likewise, if Bush is looking for a "worldwide terrorism network," he has to look no further than his own backyard--in a country that has backed murderers and strongmen around the globe.</P>
<P>Bush's claim that the "war on terror" is about "preserving democracy and freedom" is wearing thin--especially as the administration openly debates how much and what type of torture is appropriate to use with captured al-Qaeda suspects like Mohammed.</P>
<P>The administration has exploited the September 11 attacks to launch an all-out assault on our civil liberties--from its expanded ability to snoop under the USA PATRIOT Act, to the arrests and deportations of Muslim and Arab immigrants whose only crime is having the wrong skin color.</P>
<P>And Bush is using the memory of September 11 to advance his war drive against Iraq--even though his administration has failed to even manufacture evidence connecting Iraq to the hijackings or to bin Laden.</P>
<P>Some opponents of war on Iraq argue that this attack is a distraction from the "war on terrorism"--and that the administration would do better to concentrate on al-Qaeda instead. But the fact is that the war on Iraq, like the series of future wars that Bush has in store for us, is what the "war on terrorism" is all about--a blank check for U.S. military aggression abroad and a war on dissent at home.</P>

