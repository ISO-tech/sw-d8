<P><font face="Times New Roman, Times, serif" size="4">Actors and stage crews honor musicians' picket lines</font> <BR>
<font face="Times New Roman, Times, serif" size="5"><b>Broadway is on strike!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Michael Ware and Peter Lamphere</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 14, 2003 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Broadway producers from the League of American Theaters and Producers were stunned March 7 when the American Federation of Musicians (AFM) Local 802 set up picket lines at 17 Broadway musicals. The producers called it "an ambush." "No one saw this happening," one told reporters anonymously. It was the first walkout by the AFM on Broadway since a 25-day strike in 1975.</P>
<P>The issue is the same--"minimums," the number of unionized musicians each Broadway theater must use for musicals. The 325 musicians want to keep the current minimums, which range from three in smaller theaters to 24-26 in the 12 largest. Producers wanted to take advantage of the atmosphere of concessions sweeping labor negotiations in the city in order to eliminate minimums altogether. </P>
<P>Earlier, the producers had complained that minimums took creative control out of their hands and placed it under union control. In reality, producers just want to fatten their profit margins. In response, 40 prominent "creatives"--composers, orchestrators and arrangers--signed a petition in defense of musicians' minimums.</P>
<P>The producers planned to use pre-recorded music for the shows to break the power of the strike. Unionized cast and crewmembers were expected to cross the picket lines and, the producers hoped, the show would go on. But shortly after 5 p.m. on March 7, a musician at <I>The Lion King</I> overheard a crew member say, "Local 1 is out of the building."</P>
<P>The message meant that the International Association of Theatrical and Stage Employees (IATSE) Local 1 had walked out. Actors Equity, the performers' union, also refused to cross the musicians' picket lines.</P>
<P>The smug Broadway big shots were floored as 17 musicals were shut down for the entire weekend, costing producers an estimated $25-30 million in lost revenue. Musicians on the picket lines were euphoric at the upset.</P>
<P>"I don't think the producers were expecting the solidarity," <I>Lion King</I> guitarist Kevin Kuhn told <I>Socialist Worker</I>. "They weren't counting on the actors going out. And I believe it's the first time IATSE and Equity have backed us. It was a real shock."</P>
<P>Actors Equity Association President Patrick Quinn addressed rallying Broadway strikers and declared, "there was no debate about whether to go on strike. If any of us in the theater family is out on strike, we are all out on strike&#133;There are rumors that the producers think actors will be the first to cross the picket lines. But our actors have told us they will stay out until this issue is resolved."</P>
<P>"We're not being greedy, we're just trying to make a living," said Warren Odez, a union drummer from <I>Thoroughly Modern Millie</I>. "The whole thing is absurd. The next thing after this will be the virtual New York Philharmonic."</P>
<P>Greater militancy on the part of the theater unions is due to the entry of corporations like Disney and Clear Channel Communications and their drive for higher profits. When asked what has changed since 1975, Local 802 President Bill Moriarity responded that, "there is increased corporate funding and increased corporate presence and more of an anti-union attitude."</P>
<P>According to guitarist Kuhn, "[The producers] were going for zero (minimums), for a free reign. They did it in Vegas, where every casino had a big house band. They eventually got rid of all the musicians, which is what they want to do here."</P>
<P>Producers gave some ground in recent negotiations, offering a new minimum of 15 for larger theaters, but the union is holding firm. The strike, which costs producers up to $120,000 per missed show, could last several weeks as no new negotiations are scheduled.</P>
<P>As a return gesture of solidarity, Local 802's Executive board voted to extend its strike fund to union workers respecting its picket line.</P>
<P>Labor leaders from the New York Central Labor Council joined a spirited union rally at SEIU 1199's union hall March 9. The strength of solidarity is quickly becoming a symbol of labor's power to stop corporate greed and the attacks on workers--a lesson that New York's faltering labor movement badly needs as Mayor Michael Bloomberg prepares to lay off thousands of city workers. </P>
<P>But as Broadway musicians, stagehands and actors have shown, these attacks can be stopped. Solidarity is not just a slogan--it's our most powerful weapon.</P>


