<P><font face="Times New Roman, Times, serif" size="4"><b>Rich getting richer off the backs of the poor</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>Billionaire Mayor Michael Bloomberg of New York City is demanding that city workers give up $600 million in labor, pay and benefits--while he refuses to raise taxes one penny on his rich friends to meet the budget crisis that happened because taxes on the rich had previously been cut so much. It's the same old case of the rich getting richer off the backs and misery of the poor. </P>
<P>It's amazing how they're trying to get away with this. Watch the bouncing ball. That's what they have the country doing--by worrying about so-called terrorism from abroad. </P>
<P>Bloomberg-style homeland terrorists are doing tenfold more damage here and abroad. Bloomberg could personally give the city workers $600 million and still have more than $400 million left. In my opinion, one man having that kind of money while others die of starvation every day is a crime against all of humanity and the laws of righteousness.</P>
<P>Without dedicated soldiers standing on the front lines--fighting the real war (for social justice) and against billionaire terrorism--people like Bloomberg will be able to continue their authoritarian assault on ordinary working people.</P>
<B><P>Stanley Howard, </B>Joliet, Ill.</P>

