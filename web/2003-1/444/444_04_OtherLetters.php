<p><a name="Yes"></a><font face="Times New Roman, Times, serif" size="4"><b>Yes to education, not incarceration</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>Elizabeth Schulte's article ("Affirmative Action: Myth and Reality," February 28) does a good job in explaining why socialists should support affirmative action. Though overt, legal discrimination was mostly defeated in the 1960s, institutionalized racism remains a major problem in today's America.</P>
<P>However, Schulte misses one important point: The crunch for college admissions--felt by many students--is due in large part to a system which prioritizes incarceration over education. For example, in the past decade in California, 10 prisons have been built, as opposed to only two public universities. With massive budget cuts looming, universities and community colleges are facing drastic cuts.</P>
<P>Community colleges, which serve more minority students than the public university system, are bearing the brunt of these budget cuts. Two nearby community colleges are being closed, while those remaining will more than double their unit fees.</P>
<P>Not only should we embrace affirmative action, we must put into sharp relief the priorities of a system that builds 10 prisons for every two universities and shutters its junior colleges first.</P>
<B><P>John Green, </B>Davis, Calif.</P>

