<p><a name="Pentagon"></a><font face="Times New Roman, Times, serif" size="4"><b>Pentagon hacks get an earful</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>I work in Alexandria, Va., a bastion of former military officers and defense department employees located right next to the Pentagon. Over 6 percent of Alexandria's residents work for the Defense Department. It is the last place one would expect to find antiwar beliefs boiling over. </P>
<P>But according to the <I>Los Angeles Times</I>, more than 600 people packed a high school auditorium in February for a town hall meeting called by Rep. James P. Moran (D-Va.), and they overwhelmingly expressed antiwar sentiments. Two representatives from the Defense Department were given an earful from a raucous audience that thundered into applause whenever antiwar statements were made. </P>
<P>One veteran expressed bitterness about the fact that an administration made up of people who had never been to war was leading the nation into one. One woman, an Iraqi Kurd, stood up and said, "Saddam Hussein has gassed my family. He has cruelly bombed my brothers and sisters. I know what this man is capable of. And I am against this war. Against it! It will solve nothing."</P>
<P>A range of ideas were present within this antiwar crowd. For instance, when Moran called for our troops to be supported should they be sent into combat, no matter what people's disagreements today, he received applause. But the lack of confidence in Bush's war, even in a citadel of patriotic conservatism like Alexandria, shows the potential to fight back today. </P>
<B><P>Nihar Bhatt, </B>Washington, D.C.</P>

