<p><a name="SF"></a><font face="Times New Roman, Times, serif" size="4"><b>SF supervisor gets his own "welfare"</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>I live in San Francisco and work at San Francisco General Hospital. Most of us here live from paycheck to paycheck. Recently, it was discovered that city Supervisor Gavin Newsom, a wealthy mayoral hopeful, received $11 million in business loans from the superrich Getty family--without ever declaring it to the city's Ethics Commission.</P>
<P>Newsom recently sponsored the so-called "Care Not Cash" Proposition N--which cut welfare checks to 2,700 adults. Is this business as usual? If so, we might as well have the former CEOs of Enron and WorldCom running for political positions in San Francisco. </P>
<P>I was going to vote for Newsom for mayor, but now I have my doubts. Before we get screwed by any more greedy politicians, I, and many other voters, think Newsom should come clean. </P>
<P>With what Bush is doing to us and to the rest of the world, it would behoove all politicians to start acting with respect and decency towards common people.</P>
<B><P>Mark Lum</B>, San Francisco</P>

