<P><font face="Times New Roman, Times, serif" size="5"><b>Self-defense and the civil rights struggle</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Randy Childs</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 14, 2003 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>TELEVISION: <I>Deacons for Defense, </B></I>a Showtime original film, directed by Bill Duke, starring Forest Whitaker, Jonathan Silverman and Ossie Davis.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>ON JUNE 21, 1964, three civil rights activists were murdered by the Ku Klux Klan near Philadelphia, Mississippi. These murders, along with countless other attacks, led many within the civil rights movement to question the principle of nonviolence that guided the movement.</P>
<P>Didn't Blacks have the right to defend themselves against racist violence? In July 1964, a group of Black men in Louisiana--most of whom were veterans of the Second Word War--organized to do just that. The Deacons for Defense and Justice organized the armed defense of Black communities and civil rights activists against racist attacks.</P>
<P>The role they played in the civil rights movement is largely ignored by history books, which is why Showtime's film <I>Deacons for Defense,</I> is a welcome change of pace. The film portrays, and celebrates, the Deacons' activities in Bogalusa, Louisiana, a company town dominated by the local paper mill and Klan violence.</P>
<P>Forest Whitaker stars as Marcus, a mill worker, veteran and leader in the local Black church, who for most of his life has adopted a "go along to get along" attitude towards racism and segregation. But when police attack his daughter at a civil rights protest, he realizes he has no choice but to fight back.</P>
<P>Whitaker's powerful performance alone is worth the Showtime monthly premium. But the film's portrayal of the horrors of Klan violence, segregation in the workplace, the courage of civil rights activists and the debate within the movement between nonviolence and self-defense make it essential viewing.</P>


