<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">The Bush gang will stop at nothing to get its war on Iraq</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The real rogue state</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 14, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>LIES, SPIES, bashing and bribes. That's how George W. Bush is preparing to launch a horrific U.S. war on Iraq--with or without the approval of the United Nations (UN).</P>
<P>As <I>Socialist Worker</I> went to press, Washington was using all kinds of threats to blackmail developing countries on the UN Security Council into voting for war. Meanwhile, U.S. politicians and pundits were denouncing France with the kind of vitriol last heard during the Cold War.</P>
<P>Whatever the outcome of the last-minute "diplomacy"--bolstered by U.S. electronic surveillance of other countries' UN delegations--Washington has made it clear that nothing will stop the Pentagon's war machine. "When it's all said and done, if we have to use force, a lot of nations will be with us," Bush declared in his March 6 press conference.</P>
<P>In fact, Washington has ordered allies to "line up" as if they were part of the old pro-Moscow Warsaw Pact, according to Zbigniew Brzezinski, a hard-line former National Security Adviser during the Carter administration.</P>
<P>Bush's closest ally, British Prime Minister Tony Blair, is facing massive popular opposition to war and a rebellion within his own cabinet over the prospect of that country's participation in a war without UN approval.</P>
<P>Another backer, Prime Minister Jos&eacute; Aznar of Spain, faces even greater opposition--and is militarily irrelevant. And in Italy, right-wing Prime Minister Silvio Berlusconi could see a general strike against the war.</P>
<P>Perhaps Bush's most loyal ally is Bulgaria, which was once so pro-Moscow that it tried to join the old USSR. Now a NATO member, Bulgaria is "already cashing in commercially" for its support of Washington, the <I>New York Times</I> reported.</P>
<P>For their part, Mexico, Chile, Guinea, Angola and Cameroon are facing threats of economic punishment if they don't toe Washington's line.</P>
<P>Meanwhile, in Turkey, where the parliament voted to reject plans for allowing 62,000 U.S. troops to invade neighboring Iraq from its territory, the White House encouraged the former military dictators to intervene and demanded a new vote. "As the brazen Bush imperialists try to install a new democracy in Iraq, they are finding the old democracy of our reluctant allies inconvenient," <I>New York Times</I> columnist Maureen Dowd wrote.</P>
<P>As the U.S. prepares to launch its one-sided slaughter in Iraq, all the old excuses have been shattered. Weapons of mass destruction? Inspectors have found none. Disarmament? Iraq has destroyed missiles under the supervision of the UN. Liberation? Washington sold out Iraqi Kurds to Turkey--and plans to set up a military dictatorship of its own after what it expects to be a brief one-sided war.</P>
<P>"People are going to die," Gen. Richard Myers, chair of the Joint Chiefs of Staff, told reporters March 3. "As hard as we try to limit civilian casualties, it will occur. We need to condition people that that is war. People get the idea this is going to be antiseptic. Well, it's not going to be."</P>
<P>The real reason for this war is oil and empire--"the division and re-division of the world," as the Russian revolutionary Vladimir Lenin put it during the First World War. Then, as now, war exposed the barbarism beneath the civilized veneer of capitalist society.</P>
<P>An antiwar pamphlet written then by the German revolutionaries Rosa Luxemburg, Karl Liebknecht and Franz Mehring rings true today. "Shamed, dishonored, wading in blood and dripping with filth, thus capitalist society stands," they wrote. "Not as we usually see it, playing the roles of peace and righteousness, of order, of philosophy, of ethics--as a roaring beast, as an orgy of anarchy, as a pestilential breath, devastating culture and humanity. So it appears in all its hideous nakedness."</P>
<P>Today, the mightiest military machine the world has ever known is gearing up to use its full force against an already devastated country. We have to step up all our efforts to oppose this war--organizing in our workplaces, our schools and our communities, supporting every protest and every antiwar action. And we need to stand for an alternative to a system that produces horrors like the looming slaughter in Iraq.</P>


