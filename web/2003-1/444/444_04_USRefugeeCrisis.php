<P><font face="Times New Roman, Times, serif" size="4">Even immigrants with legal status are fleeing</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Refugees from the INS</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 14, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Today, I drove a Pakistani family to our local mall to find warm winter coats for their next home in Montreal. This family is among the more than 1,000 Pakistanis fleeing the U.S. to escape harassment under the newly established National Security Entry-Exit Registration System.</P>
<P>Eventually men from 25 mostly Muslim nations will be required to report to the INS to be fingerprinted, photographed and interrogated about their status. While officials claim that those whose visas and green cards are in order have nothing to fear, reports of long detentions and swift deportations suggest otherwise. </P>
<P>The U.S. government's witch-hunt has convinced even those Pakistanis with legal status to seek asylum in Canada.</P>
<P>Overwhelmed by the hundreds of people arriving at the border in advance of the February 21 deadline for Pakistanis, Canadian officials have turned many refugees back, giving them intake appointments for various dates in March. Some men have been subsequently detained by U.S. border guards, leaving their families stranded. </P>
<P>Others have made their way to towns in northern New York and Vermont, including Burlington, where refugee services and area churches have struggled to meet the need. Shelters are filled, and many individuals have opened their homes, cooked meals, or offered use of their showers and washing machines. </P>
<P>We collect sweaters and scarves for the Canadian winter. We take kids to the arcade for an hour of fun. We are also frustrated by our inability to give these families even a fraction of what they need--let alone new homes, new jobs and freedom from escalating persecution. And a new wave of refugees is expected soon. The next deadline, for men from Indonesia, comes in March.</P>
<P>All of us who are shocked and outraged by what Bush's "war on terrorism" has done to these families need to organize to put an end to this racist registration system--and to Bush's war that's turning U.S. residents into refugees.</P>
<B><P>Nancy Welch,</B> Burlington, Vt.</P>

