<P><font face="Times New Roman, Times, serif" size="5"><b>When revolution put an end to a war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | March 14, 2003 | Page 6</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE OUTBREAK of the First World War in Europe in 1914 plunged the world into a horrific convulsion of violence. In all, some 10 million people died as a result of the war, and 5 million more became refugees. Two million men fought at Verdun, the war's longest battle, and half of them were killed. Yet at the end, neither side won any substantial ground.</P>
<P>Standard history books teach that the shooting of Archduke Ferdinand in Sarajevo started this slaughter. But in reality, the cause of the war was the growing economic power of a number of industrial economies jostling to expand beyond their national boundaries.</P>
<P>Of course, no government stated its goals so crudely. The rulers of each nation cloaked their aims in nationalism, portraying the war as a noble cause to "defend the fatherland" from "outside aggressors."</P>
<P>The propaganda campaign worked at first. Many workers went off to war with a sense of adventure and honor. In Britain, 750,000 volunteered to fight in the first two months--and 1 million more signed up during the next year.</P>
<P>In convincing workers to join the war effort, the ruling classes of Europe got help from an unexpected source--the Second International alliance of socialist parties, which represented 3 million workers.</P>
<P>Just days before war broke out, the German Social Democratic Party (SPD)--the largest and most influential socialist party in the world--called on workers to reject the war drive. "In the name of humanity and of civilization, the class-conscious proletariat of Germany protests fervently against these criminal machinations of the warmongers," read the SPD's statement.</P>
<P>But when it came time for SPD members in parliament to vote on financing the war, every single one did. In country after country, this shameful betrayal of socialist principles helped rulers drum up working-class support.</P>
<P>But after years of dirt, blood, hunger and death, mass support for the First World War was transformed into its opposite--rebellions, revolts and revolutions against the governments that forced millions to slaughter each other on the battlefield.</P>
<P>Even early on, there were inspiring expressions of internationalism. Soldiers on the front lines, for example, would defy their officers' orders and forge informal truces to avoid killing and being killed.</P>
<P>"Everything is done to maintain, if at all possible, the calm," explained a soldier. "If one knocks out the opponents' bunkers&#133;the other does the same, and both lay exposed under the open sky, and no one has won anything."</P>
<P>Soldiers' resistance to their commanders was echoed at home by workers who faced rising prices for food and clothing, while industrial capitalists--especially munitions producers--made huge profits from the slaughter.</P>
<P>If ever there were a successful antiwar movement, the Russian Revolution of 1917 was it. Russian soldiers were a decisive part of the revolutionary struggle that swept away the hated tsarist dictatorship and cut short Russia's disastrous losses in the war. In Italy, Britain, France and elsewhere, antiwar protests grew alongside waves of strikes. In Germany, a revolution toppled the Kaiser and raised the specter of workers' power.</P>
<P>Unfortunately, only in Russia--where socialists in the Bolshevik Party took the lead--did a workers' revolution succeed and survive for any length of time. But this massive revolt against war--and the system that produces it--should serve to inspire us today.</P>


