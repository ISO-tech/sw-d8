<P><font face="Times New Roman, Times, serif" size="4">Hell-bent on a war on Iraq</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>And he doesn't care how many die</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">March 14, 2003 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THEY JUST don't care. That's the truth about the Bush gang and their hell-bent drive for a war on Iraq.</P>
<P>They don't care that at least 100,000 Iraqis will die, by United Nations (UN) estimates--and 100 times that number will suffer because of it. They don't care that hundreds of millions of people around the globe oppose them.</P>
<P>They don't care that the only way the U.S. might still win approval for a new UN Security Council resolution authorizing war would be through naked bribery and extortion that would shame the Godfather.</P>
<P>For the Bush administration--and its smirking moron of a commander in chief--it's full steam ahead for slaughter, with or without UN approval. That's why--against all reason, against all morality--U.S. bombers may well be carrying out their first missions during the one-week life of this issue of <I>Socialist Worker</I>.</P>
<P>"Every time we turn on the news, it is like watching a juggernaut heading towards a crowded playground in slow motion," wrote Gary Younge, columnist for Britain's <I>Guardian</I> newspaper. "We can see the catastrophe coming, but feel powerless to stop it."</P>
<P>The war on Iraq has already begun. U.S. and British warplanes have been bombing Iraq every day--under the cover of missions to patrol the U.S.-concocted "no-fly zones" over the north and south of the country.</P>
<P>Only now, according to press reports, U.S. jets are going after targets outside the zones, to cripple what remains of Iraq's air defense system--and in the hopes of provoking a response that could be spun into a pretext for an all-out assault.</P>
<P>But the real devastation is yet to come. The Pentagon is planning the most intensive bombing campaign in the history of war--firing more cruise missiles in the first day than were used in the entire 1991 Gulf War. "The bombs will still be ringing in their ears" when the U.S. Third Infantry Division rolls into Baghdad, said John Pike of the military analysis Web site globalsecurity.org.</P>
<P>How many will die? How many will be maimed? How many will see their lives wrecked--again--in a war for oil profits and expanded U.S. power?</P>
<P>George Bush and his Texas oilmen pals are ready to "liberate" Iraq--"liberate" Iraq's oil, that is. And they don't care how many Iraqis they have to kill to do it. These people are the real monsters.</P>
<P>We have to continue exposing them. Our protests--from Europe and the Middle East, to the U.S. itself--will send the message loud and clear: Stop this war!</P>


