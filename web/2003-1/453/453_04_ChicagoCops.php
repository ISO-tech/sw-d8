<P><font face="Times New Roman, Times, serif" size="4">Chicago cops' brutality caught on videotape</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Everybody's terrorized"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 16, 2003 | Page 4</font></p>

<font face="Times New Roman, Times, serif" size="3"><P><B>Dear <I>Socialist Worker,</B></I></P>
<P> Lately, the Chicago Police Department has been in the news for its brutality at the Cabrini-Green housing project. This attention has come not because the abuse is new or has gotten worse, but only because a resident caught it on videotape. </P>
<P>On April 17, 40 to 50 cops surrounded a van occupied by four men--Adam Sanders, Rondell Freeman, David Anderson and Antonio Parker. When the men would not get out, police broke the van windows, pepper sprayed the passengers, dragged them to the ground and kicked them while they were already cuffed. All this was captured on video.</P>
<P>Residents gathered to prevent an even worse fate for the men. But police beat Antonio's mother so badly that she spent four days in the hospital, where they took her in handcuffs. "I was thrown on the police car, and five police beat me," Mary Parker, a Cabrini-Green resident of 40 years who works in one of the buildings' security offices, told reporters.</P>
<P>Chicago Police Chief Terry Hillard says, "I'm personally upset by what I've seen." But the words from the top brass and the actions of cops on the beat are two very different things. </P>
<P>In fact, since the incident, police have retaliated against 18-year-old Adam Sanders, re-arresting him after beating him once again. At a demonstration to protest this brutality that I attended at Cabrini-Green in early May, there were almost as many cops as there were protesters--and they were standing in the entrance to every building in the housing project. </P>
<P>Their presence clearly was not only meant to intimidate the people marching, but to discourage residents from coming out of their apartments.</P>
<P>At the demonstration, Mary Parker told me that police brutality and intimidation is routine at the housing complex. She estimates that there are two to three incidents of police brutality every week at the complex. Often, the brutality involves juveniles. "[They] slapped an 11-year-old," she said. "They said he had too much money in his pocket. They do this every time...Everybody's terrorized, scared to death."</P>
<P>If the police have their way, there'll be more terror. Chicago police recently announced that they will be dramatically increasing the presence of officers in some of the city's "high-violence" neighborhoods. But as the attack in Cabrini Green shows, it's the cops who perpetrate racist violence everyday. We can't let them continue to get away with this terror. </P>

<P><B>Julien Ball,</B> Chicago</P>

