<P><font face="Arial, Helvetica, sans-serif" size="2"><b>THE MEANING OF MARXISM</b></font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Bringing back the old days of empire</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 16, 2003 | Page 9</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>THE IDEA that the United States should cast itself in the image of the old colonial empires is sprouting like weeds from right-wing journals and think tanks.</P>
<P>One of its most avid exponents is, fittingly, a citizen of a former empire who has some advice on how the U.S. should run its empire. Scottish historian Niall Ferguson, who has written a new coffee-table book entitled <I>Empire</I>, approvingly quotes right-wing author Max Boot, who calls for "the sort of enlightened foreign administration once provided by self-confident Englishmen in jodhpurs and pith helmets."</P>
<P>Just what did Britain's "enlightened" colonialism entail? We can start with slavery. </P>
<P>In the 18th century, British merchants were the principle shippers of slaves in the world. British colonies in the West Indies brutally exploited slaves on sugar plantations on Jamaica, Trinidad and other islands. It is roughly estimated that 12 million Africans were brought to the "new world" as slaves. The reason the demand of slaves was insatiable is because the slaves were often simply worked to death.</P>
<P>The British slave colonies absorbed 1.6 million slaves in the 18th century. Conditions on the plantations were so horrible that by the time slavery was abolished there were only 600,000 slaves left. This "undisguised looting, enslavement and murder" (in Marx's words) provided much of the "primitive accumulation" that fueled the industrial revolution.</P>
<P>In India, British conquest and plunder actually set society back--just as the slave trade set Africa back. </P>
<P>The colonizers deliberately destroyed India's thriving (and superior) textile industry by imposing high tariffs on Indian manufactures. They imposed monopoly prices on goods, which drained India of its riches. "The monopolies of salt, opium, betel and other commodities," writes Marx, "were inexhaustible mines of wealth." The British overlords imposed taxes on the peasantry that netted millions of pounds. Farmers in Bengal and Bihar alone paid out 2 million pounds a year in taxes.</P>
<P>Nor was Britain averse to causing famines for profit. Between 1769 and 1770, writes Marx, "the English manufactured a famine by buying up all the rice and refusing to sell it again, except at fabulous prices."</P>
<P>Marx called the robbery of India's wealth "a bleeding process with a vengeance." He wrote that "the bones of the cotton-weavers are bleaching the plains of India." As the industrial revolution spread in Britain, India was de-urbanized and turned into a less-developed, poorer and more rural society. Whereas in 1810, 40 percent of Indians lived in towns, by 1900 only 10 percent did.</P>
<P>There was of course nothing enlightened about colonial rule. It was founded upon the notion that the colonizers were racially superior beings destined to rule over, and in some cases, simply wipe out, the "inferior" races.</P>
<P>This justified using the utmost brutality to gain control of profitable raw materials and cheap labor. In Africa, British forces armed with machine guns massacred 10,000 Sudanese in the inappropriately named Battle of Omdurman in 1898--at a cost of only 48 British.</P>
<P>It is true that the British introduced capitalist social relations into India and other parts of its empire. But this would have taken place without conquest as a result of the spread of the capitalist world market, and perhaps had a far less distorting effect. As Vijay Prashad points out in a recent ZNet article, the British built the railroad system in India "to remove raw materials to the coast, ship troops to troubled areas and return finished products to the markets."</P>
<P>In the end, the greatest "benefit" British imperialism brought to its colonies was the creation of mass movements destined to toss them out. "The Indians will not reap the fruits of the new elements of society scattered among the by the British bourgeoisie," wrote Marx, "till in Great Britain itself the now ruling classes shall have been supplanted by the industrial proletariat, or till the Hindus themselves shall have grown strong enough to throw off the English yoke altogether."</P>

