<P><font face="Times New Roman, Times, serif" size="4">Veterans denied health care</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Cruel cost of Bush's tax cuts</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 16, 2003 | Page 12</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>TWO AND a half years ago, Ernesto Tafoya turned to the Veterans Administration (VA) to treat his hearing loss and a painful back condition. He's still waiting for an appointment today. Tafoya fought in the Second World War, and he's now among the 120,000 veterans on a waiting list to see a VA doctor.</P>
<P>Since 1996, the number of patients seeking treatment from the VA grew from 2.6 million to 4.3 million--an increase of 66 percent. Over the same period, the VA budget increased at half that rate.</P>
<P>"They're going to rebuild Iraq, and they don't have money to take care of us when we get sick and old?" Tafoya fumed to the <I>Chicago Tribune</I>. "They're going to give those rich folks a tax cut, and they're not even going to help all the veterans who were promised help?"</P>
<P>When it launched its war on Iraq, the Bush administration demanded that opponents of the invasion "support our troops." But the White House didn't utter a sound when fellow Republicans in the House voted last month to slash veterans' benefits by $25 billion over 10 years.</P>
<P>Instead, Bush has been hyping his plan to cut taxes--another huge giveaway to the already filthy rich. The House version of the Bush tax cut proposal would save the typical U.S. family $217 next year--while families with incomes of more than $1 million would average an astonishing $93,500 each. And this handout to the rich comes on top of Bush's $1.3 trillion tax cut package passed in 2001.</P>
<P>The cost of these giveaways? Bush's 2001 tax cut alone could pay the whole VA budget for the next 60 years.</P>
<P>It's not just veterans who face a nightmare of paperwork and delays when they need health care. Across the U.S., the system is in crisis--though the politicians barely acknowledge it.</P>
<P>For those with coverage, the insurance bosses are cutting back on services--even as the cost of premiums jumped 12.7 percent in the last year. For the 44 million Americans without coverage, the situation is far worse.</P>
<P>In Colorado, where there are as many as 1 million uninsured, "there is virtually no system to get into a hospital outside the city of Denver," according to the <I>Denver Post. </I>"Many clinics are closed to new patients. Doctors are forced to ignore early-stage cancers because they can't afford to justify treatment that is not an emergency."</P>
<P>Dr. Judith Wilson doesn't want to overuse the word "crisis." "But there really is no other way to say it," she admits.</P>
<P>And like many states, Colorado is cutting back even more to close a $809 million budget deficit. Last week, the state's governor signed a bill that will drop legal immigrants from Colorado's Medicaid program. Aleksandr Nukhman, a 79-year-old Russian Jew who depends on Medicaid to cover radiation treatment for his prostate cancer, fainted when he heard the news that he would be cut off.</P>
<P>The truth is that there's no reason for these health care nightmares--for veterans or anyone else. The resources are there--by taxing the rich to pay for a national system that guarantees health care to every person in the U.S. But the politicians would rather reward their rich friends, even if it means pain, suffering and death for the disabled, elderly and poor.</P>

