<P><font face="Times New Roman, Times, serif" size="5"><b>NYC transit bosses caught cooking the books</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 16, 2003 | Page 4</font></p>

<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker</B></I>,</P>
<P>Reports released a few weeks ago by city and state comptrollers revealed Enron-style accounting on the part of the New York City transit bosses. The Metropolitan Transportation Authority (MTA) has been accused of hiding up to $700 million as part of an effort to drive through a massive fare hike for millions of New Yorkers that depend on public transportation.</P>
<P>State Comptroller Alan Hevesi found that the MTA kept two sets of books--one for the public and one for internal use. An audit of the internal books found that the agency shifted millions of dollars from 2002 and 2003 accounts to 2004. </P>
<P>This accounting trick turned a 2003 surplus of $83 million into a $236 million deficit. Another city audit found hundreds of millions of dollars worth of concealed funds and overstated expenses on the part of the NYC division of the Authority.</P>
<P>The release of these audits is only the latest episode in a long history of financial wrongdoing at the MTA. Inflated costs to the tune of $430 million for renovating the agency's downtown headquarters (including a $6 million bathroom) sparked investigations into charges of bribery and sweetheart deals.</P>
<P>But the MTA is refusing to back down. Officials insist that their accounting is "legitimate" and that they will go ahead with the fare hikes anyway.</P>
<P>Working-class New Yorkers are being made to pay the price with a fare bump from $1.50 to $2.00, one of the highest base fares in the country. The hikes will also increase commuter rail fares by 25 percent on average. </P>
<P>Last year, the agency used the projection of an upcoming deficit to hammer the Transit Workers Union (TWU) in contract negotiations. As TWU Vice President Tim Schermerhorn put it, "Not one member was surprised that the MTA keeps two sets of books."</P>
<P>Some TWU members and officers have said that the union and the MTA should reopen the last contract in light of this latest scandal. Despite the outrage expressed throughout New York by riders and transit workers, New York Gov. George Pataki and New York City Mayor Michael Bloomberg have both pledged their support for the agency. In fact, Pataki--who appoints the majority of the MTA board--admitted knowing of the agency's undeclared millions.</P>
<P>Pataki's billions of dollars worth of cuts in state subsidies to public transportation and Bloomberg's proposed cut of another $200 million makes it clear how little these politicians care about decent and affordable transportation for New Yorkers.</P>

<B><P>Hadas Their</B>, New York City</P>

