<P><font face="Times New Roman, Times, serif" size="4">A high-rolling ideologue</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Mr. Virtue's vice</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 16, 2003 | Page 2</font></p>

<P><font face="Times New Roman, Times, serif" size="3">RIGHT-WING William Bennett doesn't know when to fold 'em. The holier-than-thou commentator and author of numerous books, including <I>The Book of Virtues</I>--a super-moralistic "family values" rant that attacks people for their many "vices"--was exposed last week for having gambled away millions of dollars.</P>
<P>According to reports in <I>Newsweek</I> and <I>Washington Monthly</I>, Bennett is a high-roller who lost more than $8 million at slot machines and video poker over the past decade.</P>
<P>Bennett's fellow conservatives have had strong words in the past for his preferred vice. Former Christian Coalition head Ralph Reed, for example, called gambling "a cancer on the American body politic" that was "stealing food from the mouths of children." Another of Bennett's pals, Focus on the Family President James Dobson, issued a statement saying, "Our longtime friend, Dr. Bill Bennett, is dealing with what appears to be a gambling addiction."</P>
<P>But "Mr. Virtue" doesn't seem to know the meaning of the word "shame." He lashed out at reporters who questioned him about his apparent double standard, declaring, "I play fairly high stakes. I don't put my family at risk, and I don't owe anyone anything." Of course, the reason Bennett doesn't "owe anything" is because he's made such a good living preaching about other people's sins.</P>
<P>Eventually, Bennett seems to have gotten tired of the bad press. "I have done too much gambling," he said in a recent statement, "and this is not an example I wish to set. Therefore, my gambling days are over."</P>


