<P><font face="Times New Roman, Times, serif" size="4">Detained Iraqi scientist exposed use of DU</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Framed by the Pentagon?</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 16, 2003 | Page 2</font></P>

<P><font face="Times New Roman, Times, serif" size="3">THE U.S. military announced the capture of Iraqi scientist Huda Ammash--dubbed "Mrs. Anthrax" by reporters--to great media fanfare. According to an unnamed Pentagon official, Ammash "has intimate knowledge of the workings of Iraq's biological warfare program [and is] in a position to know possible locations of where material or production facilities might be located."</P>
<P>But there's another reason for Ammash's arrest that the U.S. won't talk about. Ammash was the main Iraqi scientist to research and document the toxic effects of depleted uranium (DU) weapons used by U.S. forces in the 1991 Gulf War. One of her articles on the subject--which was reviewed and endorsed by a number of American, Canadian and British scientists--appeared in <I>Iraq Under Siege,</I> an antiwar anthology published by South End Press.</P>
<P>"We are outraged at the U.S.'s extra-legal detention of Dr. Ammash and its plans to interrogate her," said Alexander Dwinell of South End Press. "The U.S. government is trying to silence Dr. Ammash's outspoken criticism of the U.S. role in causing cancers and other illnesses in Iraq through its own use of biologically hazardous weapons such as radioactive DU."</P>
<P>Like its other allegations about Iraq's weapons of mass destruction, the U.S. hasn't released any evidence to back up its charges against Ammash. Even UNMOVIC--the United Nations team of weapons inspectors in Iraq--has no information that would substantiate the U.S. allegations. "UNMOVIC did not single Dr. Ammash out for interviews because UNMOVIC did not have clear evidence to link Dr. Ammash to [biological weapons] programs," according to Hiro Ueki, spokesperson for the inspectors.</P>
<P>Felicity Arbuthnot, an author and activist who was the senior researcher for antiwar journalist John Pilger's award-winning documentary about the effects of sanctions in Iraq, also condemned the detention.</P>
<P>"I have spoken with her on several platforms about DU and am deeply suspicious at U.S. and UK troops allowing the destruction of all hospital records--thus every record of the health effects linked to DU since 1991--and now the detention of one of the most knowledgeable and passionate experts on it," Arbuthnot said. </P>
<P>As she concluded: "I find it deeply disturbing--beyond outrage actually--that the whole of Iraq is becoming a vast Guant&aacute;namo Bay, with POWs and officials being 'disappeared' with no legal representation. The U.S./UK 'alliance' have become the rogue state."</P>

