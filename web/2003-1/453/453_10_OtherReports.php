<p><a name="Racism"></a><font face="Times New Roman, Times, serif" size="4"><b>Fighting racism at New York University</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Candice Amich</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--In response to a string of racist hate mails sent out over the New York University (NYU) Stern School of Business administrative list serve, students called for an emergency town hall meeting on May 5. More than 200 NYU students came together to figure out how to respond to this hate crime and NYU officials' inadequate response to the hackers' white supremacist spree.</P>
<P>Many students testified to racist encounters they had experienced on campus, saying that the racist e-mails were an escalation of a more subtle racism at NYU. Of a graduating class of 700, fewer than 35 Stern graduates--or 5 percent of the graduating class--are Black or Latino. Many expressed concern about next year's 7 percent tuition increase as well as the massive city budget cuts, which will disproportionately affect minority students.</P>
<P>At the town hall meeting, students decided to form an antiracist network and create a list of demands to present to administrators. And there's a planned protest of Mayor Michael Bloomberg's speech at this year's graduation services.</P>
<P>"We want the problem of racism at NYU to be recognized," said NYU student Mercedes Thurman. "We have some numerical diversity at NYU, but we're not integrated. We're still living in a very segregated society."</P>

