<P><font face="Times New Roman, Times, serif" size="4"><b>Defend Palestinian rights</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Desmond Gardfrey and Jonathan Wexler</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>ATLANTA--About 75 people came to hear speakers from the Voices for Justice Tour, sponsored by International Solidarity Movement-Palestine and the Unitarian Universalists for Justice in the Middle East. Speakers included Atlanta resident Elizabeth Corrie, whose cousin Rachel was murdered by an Israeli bulldozer that crushed her while she was attempting to protect a Palestinian family's home.</P>
<P>"Rachel always stood on the side of the oppressed," said Elizabeth, who told the crowd that Rachel had spoke to the Washington state legislature to advocate an end to world hunger when she was just 10 years old.</P>
<P>Rachel led a walkout in support of a teacher's strike in seventh grade and was active in the antiwar movement since September 11. "She had to go to Palestine--it was her character," Elizabeth said.</P>
<P>Vivien Samsour, who was born in Palestine, described the daily repression and violence she faced at the hands of the Israeli military. "Going on the balcony is risking getting shot," she said. Vivien said that when she was boarding a flight to the U.S., Israeli agents pulled her out of line, strip-searched her and wouldn't let her kiss her mother goodbye.</P>
<P>Israeli-born Jewish activist and spoken word artist Ora Wise conveyed the urgency of fighting the right wing who are dominating Israeli politics. The Voices for Justice Tour tour will resume later this year.</P>
<I><P>Go to www.palsolidarity.org to find out how to bring the tour to your town.</I></P>

