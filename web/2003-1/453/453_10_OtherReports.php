<p><a name="Liberties"></a><font face="Times New Roman, Times, serif" size="4"><b>Defend civil liberties</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Wengraf</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--"The war is not over!" declared activists at Operation Homeland Resistance, three days of civil disobedience action targeting the Immigration and Naturalization Service. </P>
<P>Declaring their opposition to the "war on terrorism," the scapegoating of Muslims and Arabs, and the occupation of Afghanistan and Iraq, more than 80 activists were arrested at actions during the three days. "We wanted to send a message about the war abroad and the war at home," Homeland Resistance spokesperson Wol-San told <I>Socialist Worker. </I>"The experience of people-of-color communities is symbolic of both wars."</P>
<P>Operation Homeland Resistance attacked the "war on terrorism" as a thinly veiled attempt to justify detentions, "special registrations" of Arabs, attacks on civil liberties and cuts to social programs.  As Monami Maulik of the immigrant rights' group Desis Rising Up &amp; Moving put it, the growing number of immigrant detentions and deportations dates to 1996 and President Clinton's Immigrant Responsibility Act. "More than 1 million immigrants from 125 countries have been deported since 1996," said Maulik.</P>
<P>Activists from Racial Justice 911 and Third World Within, two groups involved in the action, plan to keep up the pressure with a May 19 protest in Times Square against the U.S. occupation of Iraq. </P>
<I><P>For info on upcoming actions, e-mail info@homelandresistance.org.</i></P>

