<p><a name="Hospitals"></a><font face="Times New Roman, Times, serif" size="4"><b>Los Angeles hospitals</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Evan Kornfeld</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>LOS ANGELES--Emergency room patients at the County-USC hospital--the city's largest public hospital--regularly wait as long as four days for a bed, according to a sworn statement by Dr. Edward Newton.</P>
<P>Dr. Newton is the emergency room's interim chairman, and he was testifying as part of a lawsuit by legal aid groups to stop Los Angeles County from eliminating 100 of the 745 beds at the hospital. County-USC treats 600,000 uninsured patients every year in its severely overcrowded emergency room and inpatient facilities. </P>
<P>Dr. Newton recounted the case of a 40-year-old woman with an arterial blockage in her lungs who was kept in the emergency room for 30 hours. She died from cardiac arrest while waiting for a bed. He also told of a man with shortness of breath who died before doctors could help him. "Had we had the capacity to diagnose him earlier," said Dr. Newton, "his life would have been saved."</P>
<P>Dr. Ronald Kaufman, a former chief medical officer at County-USC, wrote that the proposed cuts slated to begin in June would "destroy" the hospital and the trauma system in Los Angeles County. </P>
<P>In a related case, a federal judge issued a tentative decision to block the closure of Rancho Los Amigos National Rehabilitation Center in Downey. The center treats 9,500 patients a year, mostly people with brain or spinal injuries. About half its patients are covered by Medi-Cal, California's version of Medicaid. </P>
<P>Judge Florence Marie Cooper concluded that if the center closes, these patients "will not be able to obtain adequate substitute care at surviving hospitals or other settings." The county announced it will appeal her decision.</P>
<P>People are being denied adequate health care while the government funds its occupation of Iraq. It's time for people to organize and stop this madness.</P>

