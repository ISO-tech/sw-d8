<p><a name="Labor"></a><font face="Times New Roman, Times, serif" size="4"><b>University of California labor solidarity</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By John Green</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>SANTA CRUZ, Calif.--About 60 students from across California met at University of California (UC)-Santa Cruz May 3 and 4 to foster student activism around university labor organizing. Julia Harumi, an immigrant rights lawyer, summarized the activist tone: "Lawyers can't win fights--it has to be the workers."</P>
<P>Students discussed strategies and shared stories of successful campaigns in meetings such as "Building Workplace Power" and "Organizing for Victories in Budget Cuts." "There's a connection between our imperialist adventures abroad and the assault on workers at home," said Peter Olney of the Institute for Labor and Employment.</P>
<P>This conference, the first of its kind representing all of the UC system campuses, is a much-welcomed step by organized labor to turn back the relentless attack during the past 20 years. "&#191;Se puede?" asked one AFL-CIO organizer. "&#161;S&iacute;, se peude!"</P>

