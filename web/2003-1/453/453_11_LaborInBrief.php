<P><font face="Times New Roman, Times, serif" size="4"><b>Azteca Foods</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font></P>

<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--After seven months on strike, workers at Azteca Foods voted unanimously to return to work last week. The workers--members of United Electrical Workers Local 1159--decided to end the strike and continue their fight for a new contract from inside the plant.</P>
<P>During the strike, not a single striker crossed the picket line--"a point of pride for the returning workers and an impressive display of long-term unity," according to a statement by the union. Also contributing to the decision to return to work was Azteca's recent decision to settle unfair labor practice charges filed by UE Local 1159 that Azteca "illegally threatened to fire workers for union activity and that it violated their rights though intimidating interrogations and surveillance." Azteca didn't admit any guilt, but did agree to post announcements in the plant that it will obey U.S. labor laws in the future.</P>
<P>The strike began last September when workers asked United Electrical Workers (UE) Local 1159 to represent them, and Azteca owner Arthur Vel&aacute;squez refused to acknowledge the union. For decades, the Azteca workers--all of whom are Latino immigrants--were represented by a corrupt, mob-connected union. When Vel&aacute;squez pushed for a 700 percent increase in health insurance co-pays while offering raises of 5 cents, workers had enough.</P>
<P>Workers took the fight to the streets, even picketing LaSalle Bank where Vel&aacute;squez sits on the board. And despite the return to work, Azteca workers voted to continue the boycott--a tactic that the union estimates has cost Azteca some $15 million.</P>

