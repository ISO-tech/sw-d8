<P><font face="Times New Roman, Times, serif" size="5"><b>Broken promises on fighting AIDS</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | May 16, 2003 | Page 2</font></P>

<font face="Times New Roman, Times, serif" size="3"><P>THERE'S NO such thing as "no strings attached" when it comes to the Bush administration. Back in January, when he delivered his State of the Union address, George W. Bush promised $15 billion over five years to fight the spread of HIV and AIDS in Africa and the Caribbean. The Emergency Plan for AIDS Relief would be, according to Bush, "a work of mercy beyond all current international efforts to help the people of Africa."</P>
<P>In reality, $15 billion is well short of what experts say is needed to halt the spread of the disease. And later press reports revealed that Bush's promise of additional funding helped the administration to get its guy--Health and Human Services Secretary Tommy Thompson--appointed to head the Global Fund to Fight AIDS, Tuberculosis and Malaria.</P>
<P>Actually, the administration will contribute just $200,000 a year to the Global Fund. "Secretary Thompson will be chosen to chair the board of the Global Fund to Fight AIDS a day after a generous presidential AIDS initiative largely sideswiped the Global Fund," Asia Russell, director of international policy at AIDS lobby group Health GAP, said in a statement. "If the U.S. is going to buy the chairmanship, they could at least use real money."</P>
<P>Now, right-wing hacks in Congress are trying to make Bush's $15 billion promise mean even less.</P>
<P>According to the Sexuality Information and Education Council of the U.S. (SIECUS), the House version of global AIDS legislation added two amendments that will cost countless lives. One amendment sets aside one-third of the bill's overall prevention dollars for totally ineffective abstinence-only programs. A second amendment allows faith-based organizations that receive funds to be exempt from discussing prevention methods--such as condom use--in "conflict" with religious teachings. </P>
<P>"These amendments directly contradict what science says works best to prevent HIV/AIDS--a broad comprehensive approach that addresses multiple factors, including the use of condoms," said SIECUS President Tamara Kreinin in a statement. "The U.S. is exporting policies born of conservative ideology, not public health."</P>

