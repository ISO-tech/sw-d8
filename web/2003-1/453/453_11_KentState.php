<P><font face="Times New Roman, Times, serif" size="5"><b>Kent State police brutality</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">May 16, 2003 | Page 11</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>KENT, Ohio--Thirty-three years after National Guardsmen opened fire on Vietnam antiwar protesters, police are still handing out repression at Kent State University. Every year, on the event's May 4 anniversary, student activists commemorate the death of the four students in 1970. This year, activists used the date to demand an end to the U.S. occupation of Iraq in a protest organized by the Kent State Anti-War Committee and Not In Our Name.</P>
<P>A few days before the protest, university officials revoked the permit because they said they feared "violence or disruption." Despite the ban, about 300 people showed up to protest.</P>
<P>After the rally, protesters marched to the parking lot where the four students were killed in 1970. From there, marchers went to the front of campus. By this time, police in full riot gear had amassed, blocking off streets surrounding the campus.</P>
<P>When protesters tried to march--on the sidewalk--riot cops began picking protesters out of the crowd, throwing them to the ground and arresting them.</P>
<P>A woman who had known Jeffery Miller, one of the students killed in 1970, was there to pay her respects when she was arrested, put on a bus and shoved into a seat. When she tried to stand up, several cops pushed her head against the bus window, cracking the window and causing a large bump on her head. Then they charged her with felonious vandalism for breaking the window.</P>
<P>Activists organized a vigil that evening outside the Kent police station to demand the release of 14 people who had been arrested. The following week, the Kent City Council voted to investigate the police.</P>
<P>This shocking attack on people who were simply expressing their freedom of speech is part of the new wave of government repression since September 11. We cannot afford to be intimidated. Now more than ever, we must speak out.</P>
