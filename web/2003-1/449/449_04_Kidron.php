<P><font face="Arial, Helvetica, sans-serif" size="2"><b>OBITUARY</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Michael Kidron</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 18, 2003 | Page 4</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MICHAEL KIDRON, longtime British socialist and theorist, died last month in London at the age of 72.</P>
<P>Even for those of us who never met him, Kidron's contribution to our political education is greatly appreciated. As an editor and frequent contributor to the <I>International Socialism </I>journal in the 1960s, Kidron was central to the project of making revolutionary socialist politics relevant to a new generation of radicals.</P>
<P>Kidron's work reflected the best of the International Socialist tradition--seeing theory not as an academic pursuit, but as a way to generalize and apply Marxism to the changes in the world after the Second World War, even when it challenged orthodoxy--as in developing the theory of state capitalism in Russia.</P>
<P>Kidron's article, "A Permanent Arms Economy" remains the best summary explanation of how the Cold War arms race fueled the economic boom of the 1950s and 1960s in the U.S. and Western Europe. Yet rather than seeing the boom as permanent, as was the fashion of the day, Kidron's book, <I>Western Capitalism Since the War,</I> anticipated the economic crisis that emerged in the early 1970s.</P>
<P>Born in South Africa into a Zionist family, Kidron moved to Palestine shortly before the founding of Israel. He followed his sister, Chanie Rosenberg, and her husband, Tony Cliff, to London years later, and quickly became central to the founding of the Socialist Review group, the forerunner of the International Socialists and, later, the British Socialist Workers Party.</P>
<P>Kidron left the organization in the 1970s over theoretical political differences. He continued to work as a writer and editor at Pluto Press, producing <I>The State of the World Atlas</I> and other resources for activists.</P>
<P>Kidron's contributions to Marxism stand the test of time. And his conclusion in a 1965 article remains relevant today: "The greatest service we can render international socialism is to help stoke up the fires at home."</P>


