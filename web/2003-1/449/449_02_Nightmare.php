<P><font face="Times New Roman, Times, serif" size="4">"The situation in Iraq is chaotic and catastrophic"</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>The nightmare the U.S. caused</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 18, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH claims that the U.S. has liberated Iraq from Saddam Hussein's rule. But he won't say how many ordinary Iraqis have been "liberated" from food, water, electricity and medical care.</P>
<P>Iraq is facing a humanitarian disaster of huge proportions. According to the International Committee of the Red Cross, Baghdad's medical system has "virtually collapsed" from a combination of damage from U.S. bombs, looting and the previous devastation of economic sanctions. </P>
<P>The organization said last week that it doubted any hospital in Baghdad--a city of more than 5 million people--was still working. "The situation is chaotic and catastrophic," Red Cross medical coordinator Peter Tarabula told Agence France Press after surveying the Al-Kindi hospital in Baghdad.</P>
<P>Hospitals have run out of a wide array of medicines and anesthetic--forcing some doctors to perform surgeries with little more than aspirin to give patients. And since the fall of Baghdad, many hospital workers stopped reporting for work after looters invaded some hospitals and stole vital equipment--while U.S. soldiers looked on. </P>
<P>According to Britain's <I>Guardian, </I>at Yarmouk hospital, formerly the city's main casualty center, "floors were coated with stale blood, and wards stank of gangrene. The wounded lay on soiled sheets in hospital lobbies, screaming with pain, or begging for tranquilizers. Orderlies in blue surgical gowns shouldered Kalashnikovs to guard against marauders. Ambulance drivers staged counter-raids on looters to reclaim captured medicines and surgical supplies."</P>
<P>Rotting corpses were left exposed until they could be buried in mass graves by volunteers. So many have died, in fact, that the Red Cross says there is a shortage of blankets--because they have been used as body bags. </P>
<P>Beyond the medical crisis, finding the basic necessities of life is harder and harder. In southern Iraq, a severe water shortage has forced some desperate civilians to drink from drainage ditches. Although UNICEF got a few water tankers into the cities of Umm Qasr, Zubayr and Basra, officials admit that it's nowhere near enough to make a dent in the crisis.</P>
<P>In Basra, the water supply is functioning at 30 percent of capacity because of bomb damage to the city's water treatment facilities. "The clock is ticking, and if time runs out, we'll have a huge problem like you've never seen before," said UNICEF spokesman Marc Vergara.</P>
<P>And as looting continued in Baghdad, food is running low, and water and electricity have been cut. Sixty percent of Iraqis were dependent on the government food basket for the bulk of their diet before the war. Now, with that system in disarray, starvation is looming.</P>
<P>Humanitarian groups now face the biggest task of reconstructing food distribution in history, but they haven't been able to begin work--because of the looting and disorder taking place in cities like Basra and Baghdad.</P>
<P>But don't try telling the Bush administration that it has any responsibility for the chaos. "[F]reedom's untidy," Defense Secretary Donald Rumsfeld smirked last week. "And free people are free to make mistakes and commit crimes and do bad things."</P>
<P>That won't be much consolation to Baghdad resident Arkan Daoud Boutros. "Americans entered the city with the slogan of helping us, but we haven't seen anything from them," he told Britain's <I>Independent</I> last week. "We have seen only robbery."</P>


