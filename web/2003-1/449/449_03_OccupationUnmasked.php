<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>U.S. occupation of Iraq unmasked</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 18, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE TOPPLING of a statue of Saddam Hussein in Baghdad last Wednesday provided the media with the perfect image to cheer the U.S. "victory" in the war on Iraq. But there was a lot more to the picture than met the eye--literally.</P>
<P>The fall of the statue unleashed an overwhelming tide of propaganda designed to batter anyone who had questioned the war into accepting that the invasion of Iraq really had been about "liberation." In the days that followed, no news report was complete without stories about grateful Iraqi people hugging U.S. soldiers and declaring that "Bush is great!"</P>
<P>Within a few days, the horror of the chaos caused by the U.S. invasion started to re-emerge. Nevertheless, the antiwar movement shouldn't be shy about answering the claim that Iraqis were, in the end, glad that the U.S. toppled Saddam Hussein.</P>
<P>First, the pictures of cheering Iraqis were deceptive about the scale of the "celebrations"--sometimes laughably so. For example, pictures began to emerge of the toppling of the statue of Saddam in Baghdad--only from a wider angle. They showed the fact that only a few hundred Iraqis had been on hand for the "historic moment."</P>
<P>Donald Rumsfeld and others compared this to the fall of the Berlin Wall in 1989. But millions of people were involved in the revolutions that toppled the regimes of Eastern Europe--and they didn't need a Bradley fighting vehicle to help them pull down the hated statues of Stalin and other symbols of the ex-USSR.</P>
<P>There were few tears shed for Saddam Hussein as the U.S. took over Baghdad last week. After all, most Iraqis have hated him for much longer than the U.S. government has. Saddam was personally singled out by the CIA in the late 1950s to participate in a coup that propelled the Baath Party to power--and he met with none other than Donald Rumsfeld when Washington decided in the 1980s to support Saddam against neighboring Iran, not to mention the Kurds and Shiites within Iraq.</P>
<P>The resistance among Iraqis that hamstrung the U.S. invasion early on did give way as U.S. troops reached Baghdad. The main reason for this was the failure of the Iraqi government to mount an effective defense of the capital.</P>
<P>When most of the regime's top officials fled the capital as U.S. soldiers closed in, Iraqis who were preparing to fight for Baghdad must have wondered why they should. The character of the regime--a repressive police state that has ruled with an iron fist for decades--undermined the resistance to the invasion. </P>
<P>But this doesn't mean that the resistance has ended. Within days of the "liberation" of Baghdad, the media were already having a hard time finding "celebrations" of the U.S. invasion--and more and more Iraqis were openly taking a stand against their "liberators."</P>
<P>Thus, for example, as the U.S. took control of Baghdad, a Shia Muslim leader groomed by the U.S. for a role in post-Saddam Iraq was murdered, and another was told to leave the country by Iraqi protesters furious with his ties to Washington.</P>
<P>All of the media's efforts can't erase the fact that while Iraqis hate Saddam Hussein, they hate a U.S. invasion and military dictatorship even more. The millions of people who came out on the streets to oppose the U.S. war in the weeks before it began--and the millions more who saw their own doubts reflected in the protests--were right to oppose this war.</P>
<P>The tide of propaganda will have made many feel defensive. But our job must be to stand up against the lies and distortions--and not be silenced in our opposition to Bush's ongoing war for oil and American Empire.</P>

