<P><font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 18, 2003 | Page 11</font></p>

<P><font face="Times New Roman, Times, serif" size="4"><b>SEIU Local 32B-32J</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Talks between Service Employees International Union Local 32B-32J and the Realty Advisory Board have stalled over the issue of pay raises. The union's 28,000 doormen, custodians, superintendents and elevator operators could strike at 12:01 a.m. on April 21 if no agreement is reached. </P>
<P>The workers maintain more than 3,000 buildings, primarily in Manhattan, Brooklyn and Queens. With a continued slump in stock performance, many investors have turned to real estate in New York as a better investment. </P>
<P>Real-estate value has increased despite the overall downturn in the economy. But the Realty Advisory Board is still crying poverty, claiming that the money for raises just isn't there.</P>
<P>The union has already established eight strike coordination locations with 500 picket captains. Both sides say there's a 50-50 chance of a strike.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Washington Teachers Union</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jesse Hagopian and Michele Bollinger, WTU members</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WASHINGTON--About 150 teachers chanted "No cuts, no way!" outside the city council building last Thursday to protest the recent cuts to the public education budget. The city council approved a $65 million budget cut that leaves teachers without their regular pay increase, lays off many D.C. Public School (DCPS) employees and leaves already strapped classrooms without basic supplies.</P>
<P>Crumbling schools can't withstand this massive budget cut. The school board, in turn, intensified the attack, issuing a plan to furlough 5,000 teachers for four days at the end of the school year. To add insult to injury, the president of the school board, Peggy Cooper Cafritz, called for all DCPS teachers to take a standardized test by the end of the 2003 school year.</P>
<P>Opposition to these plans is driving teachers to take a stand against them, despite the recent scandals within the Washington Teachers Union (WTU) that have left it in receivership under the auspices of the American Federation of Teachers.</P>
<P>The rally had an angry tone. When one speaker said, "Only the rich benefit from these cuts, but we will punish them," the crowd let up a huge cheer. The anger is so widespread that teachers at one school, Wilson Senior High School, independently organized a picket and march outside of their building before the school day began on Friday.</P>
<P>Seventy teachers gathered outside Wilson Senior High School to demand pay increases and no furlough. "We simply cannot allow the city council and the school board to balance the crisis in the DCPS budget on the backs of teachers," said Greg Bargemon, a librarian at Wilson.</P>
<P>If teachers are going to beat these budget cuts, we'll have to organize at our schools and within our union. As one sign at the rally said, "Listen to your teacher--don't cut school."</P>

