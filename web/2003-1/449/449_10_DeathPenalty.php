<P><font face="Times New Roman, Times, serif" size="5"><b>End the death penalty</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Danny Katch</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 18, 2003 | Page 10</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--More than 150 people came to the Riverside Church April 11 to hear powerful stories of resistance to the criminal injustice system. The meeting--sponsored by the Campaign to End the Death Penalty (CEDP)--is part of a national speaking tour by Madison Hobley, one of four men recently pardoned from Illinois death row by former Gov. George Ryan.</P>
<P>Madison is one of the Death Row 10, a group of Black men tortured by Chicago police into giving false confessions. The Death Row 10, together with the CEDP, fought for years to expose their frame-ups.</P>
<P>CEDP member Joan Parkin spoke about how this fight played a major role in Ryan's decision. Madison and his sister, Robin Hobley, spoke about their experiences fighting for his freedom.</P>
<P>Other speakers included Yusef Salaam, one of five men falsely convicted in the infamous Central Park Jogger case; his mother Sharonne Salaam; and actor Ossie Davis, who stressed the importance of building grassroots organizations.</P>
<P>Speaker after speaker connected the war on Iraq and the war on the poor and people of color in the criminal justice system.</P>


