<P><font face="Times New Roman, Times, serif" size="4">Protests continue in the wake of the U.S. victory</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"We're exposing their lies"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 18, 2003 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>DAYS AFTER the U.S. military declared that it had "liberated" Iraq, antiwar activists came out on April 12 to show their opposition to Washington's war on Iraq. About 15,000 people took to the streets of Washington, D.C., for a protest called by the antiwar group International ANSWER.</P>
<P>The turnout was smaller than previous antiwar protests organized over the last months, in part reflecting the impact of the U.S.'s so-called victory in Iraq. But for many antiwar activists at the protest, it was clear that we have to keep organizing against the U.S. war--and the brutal occupation that lies ahead.</P>
<P>"There's going to be a long bloody occupation to follow," one man told <I>Socialist Worker, </I>"and after the U.S. is done with Iraq, they're going to move to Syria and Iran--anyone who gets in the way of U.S. empire."</P>
<P>One activist expressed his disgust at the newly appointed head of Iraq's interim government--retired Lt. Gen. Jay Garner. "It just makes me sick that the U.S. tells the world that they're trying to bring democracy and then puts American military men in power," he said. "We have to be out here to expose the lies and show the rest of America who believe Iraq's been liberated that they're wrong."</P>
<P>In San Francisco, about 4,000 antiwar protesters turned out in the driving rain to march from the Civic Center to Dolores Park. In addition to the bad weather and the impact of the U.S. victory, the small turnout could also be attributed to the march organizers, ANSWER, who refused to collaborate with other organizations during preparations for the march.</P>
<P>In Portland, Ore., some 4,000 people marched through the streets. Speakers included Frank Fromherz, who was fired as the justice and peace director of the Portland archdiocese because he wouldn't stop sponsoring antiwar rallies.</P>
<P>Activists must continue to expose Bush's lies about his war to bring "democracy" to Iraq--and build opposition to this occupation.</P>
<P>In Madison, Wis., 20 people, most of them University of Wisconsin students, took over the lobby of news station NBC 15 on April 8. The direct action group, Stop Propaganda--Insist on Truth, or SPIT, dressed as United Nations inspectors and "inspected" the station for "weapons of mass deception." SPIT targeted the NBC station because of its biased coverage of the war as well as its connections with General Electric.</P>
<P>At New York University (NYU), activists are organizing a teach-in April 15 to build broader support for the demand that NYU cease its secret military contracts and disclose its investments. Peace Coalition members are also busy planning and publicizing for an April 17 rally in Washington Square Park titled "A Day for Peace and Justice."</P>
<I><P>Candice Amich, Paul Dean, Laura Nelson and Aislinn Stetson contributed to this report.</P>
