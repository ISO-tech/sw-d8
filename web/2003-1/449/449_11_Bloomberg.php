<P><font face="Times New Roman, Times, serif" size="5"><b>New York City mayor declares war on workers and the poor</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Thomas Barton, shop steward, AFSCME Local 768</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 18, 2003 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--On April 8, Mayor Michael Bloomberg announced the lay-off of 3,443 public-sector workers, mostly from the Department of Education. Leaks from City Hall claim the mayor's budget presentation this week will likely call for $1 billion more cuts to services, in addition to the $600 million already announced, with 15,000 fresh layoffs.</P>
<P>"Honoring" the memory of hundreds of firemen killed on September 11, eight firehouses will be closed and 4,000 public hospital workers will be fired. The Metropolitan Transit Authority also announced the elimination of 817 administrative and support staff positions by the end of 2003.</P>
<P>Since taking office in January 2002, Bloomberg has cut 15,000 jobs from the payroll and laid off 600 since January 2003. At Bellevue Hospital, one administrator told <I>Socialist Worker</I> that management had already gotten a list with names of workers to get pink slips. "Hell, let's just take them straight from the emergency room to the morgue," a Bellevue Hospital nurse told <I>Socialist Worker</I>. "We won't have enough people left to do treatment."</P>
<P>Adding insult to injury, the Bloomberg regime has called for extreme cuts in pay and benefits for workers that survive the layoffs. The mayor wants AFSCME District Council 37 to agree to 40 hours' work for 35 hours' pay, cuts in sick time, cuts in overtime pay, unpaid holidays, cuts in vacation time, abolition of all paid release time for union activities and making workers start to pay for health care and prescription drug plans. </P>
<P>Pay increases won't be retroactive. And because the last contract expired in July 2002, the mayor has essentially imposed at least a year's pay freeze. </P>
<P>The mayor brushed off attempts by municipal union to find $600 million in savings, such as the elimination of outsourcing, where contracts are awarded to politically connected private firms who inflate costs by making a profit. </P>
<P>"Who can help me?" asked Ivonne Miranda, an AFSCME Local 1597 custodian who has been laid off. "I'm a single parent. I don't want to go on public assistance." She is also losing health benefits for herself and her 13-year-old autistic son Harry. She has lost weight, has bouts of insomnia and has even contemplated suicide. "They tell us, Hispanics and Blacks, to vote," said Miranda. "But this is what causes civil war. The mayor doesn't care. He's rich."</P>
<P>New York City already suffers from an "official" unemployment rate of 8.4 percent--with an estimated 240,000 job losses since early 2001. Although a recent LRA Consulting poll shows that 76 percent of New Yorkers oppose layoffs to balance the budget, Mayor Bloomberg has come under no real pressure from the unions. </P>
<P>Union leaders' complaints to the press are a far cry from mobilized union members taking action in the form of slowdowns, job actions and strong outreach to other unions and communities affected by service cuts.</P>
<P>Politicians like Bloomberg and Bush are convinced that working people have no way of fighting back. Their overconfidence has led them to attack broad sectors of society all at once. Have they overplayed their hand? </P>
<P>City unions should reject the logic of concessions and rally working-class New Yorkers to fight for jobs, better education and social services. DC 37 has called a rally at City Hall Park on Tuesday, April 29, from 5:30 p.m. to 7:30 p.m. to protest the cuts. And the United Federation of Teachers plan to protest against education cuts in Albany on May 3.</P>
<I><P>Michael Ware contributed to this report.</P>
