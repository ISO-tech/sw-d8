<P><font face="Times New Roman, Times, serif" size="4">Arundhati Roy on Bush's war for American Empire:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"The outline of the beast"</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 18, 2003 | Pages 6 and 7</font></p>

<P><font face="Times New Roman, Times, serif" size="3">GEORGE W. BUSH and his gang of war makers in Washington will say anything to justify their war. They demand unquestioning support for the slaughter in Iraq, both inside the U.S. and around the world--and they want to frighten into silence anyone who disagrees.</P>
<P>But they haven't succeeded. Across the globe, tens of millions of people have taken to the streets to show their opposition to Bush's war--an unprecedented international antiwar movement.</P>
<P>ARUNDHATI ROY is one of the best-respected voices of this movement. She is internationally known as a novelist--her book <I>The God of Small Things </I>won the prestigious Booker Prize--and equally renown for her commitment to the struggle for global justice. Her latest book, <I>War Talk,</I> has just been published by South End Press. Roy talked to <I>Socialist Worker's </I>ANTHONY ARNOVE about the motives behind Bush's war--and the struggle to stop it.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><P><B>THE CORPORATE media ask the question over and over again: What can be done about Saddam Hussein? What's your response?</B></P>

<P>THE QUESTION is disingenuous. Let's turn it around and ask instead: What do we do with George Bush and Tony Blair? Should we just stand by and watch while they bomb and kill and annihilate people? Saddam Hussein is a killer, and in the past, the U.S. and the UK governments have supported many of his worst excesses. </P>
<P>The U.S. and UK have bombed Iraq's infrastructure, fired depleted uranium into Iraq's farmlands, blocked vaccines and hospital equipment, contributing to hundreds of thousands of deaths of children under five. Denis Halliday, the former UN humanitarian coordinator in Iraq, has called the sanctions a form of genocide. </P>
<P>If you lifted the sanctions, Iraqi society might have gained the strength to overthrow their dictator (just like the people of Indonesia, Serbia, Romania overthrew theirs). </P>
<P>And if it's repression, sectarianism and human rights abuses we're concerned about, let's also turn our attention to Colombia, Turkey, Saudi Arabia, Egypt, the Central Asian Republics, Israel, Russia, China, India, Pakistan, Burma and, of course, America...Shall we pre-empt Saddam and bomb them all? Then he won't have anyone left to kill. </P>
<P>The greatest threat to the world today is not Saddam Hussein, it's George Bush (joined at the hip to his new foreign secretary, Tony Blair).</P>

<P><B>BUSH SAYS that he's leading an "international coalition" against Iraq. What's your reaction to that?</B></P>

<P>THE INTERNATIONAL Coalition of the Bullied and the Bought is what that coalition is more commonly called. </P>
<P>The important thing to keep in mind is that it is governments that have been coerced, one way or another. Even the major "shareholders" in the coalition--governments of countries like Spain and Australia--don't have the support of the majority of their people. </P>
<P>There have been some interesting studies showing the nature of the regimes of some of the countries in this "coalition." Many of them are high up on the list of human rights violators--and have no business to criticize Saddam Hussein given their own reputations.</P>

<P><B>BUSH ALSO says that this war is "defensive," and that it would be "suicidal" not to attack Iraq.</B></P>

<P>THAT'S LIKE an elephant taking a long run-up to smash an ant to death--and then saying that it was "defensive," and that to let the ant remain alive would have been suicidal. It would be fair to call the elephant paranoid and unstable. </P>
<P>Oh, and that doesn't include the business of using the UN to disarm the ant before the elephant attacks. Apart from calling it paranoid and unstable, you could also call it a coward and a cheat.</P>

<P><B>IN AN interview on the Pacifica Radio program "Democracy Now!" you spoke about the "murder of language." Can you elaborate on that?</B></P>

<P>FREEDOM MEANS mass murder now. In the U.S., it means fried potatoes (freedom fries). Liberation means invasion and occupation. When you hear the words "humanitarian aid," it's advisable to look around for induced starvation. We all know what collateral damage means. </P>
<P>Of course, none of this is new. When the U.S. invaded South Vietnam and bombed the countryside, killing thousands of people and forcing thousands to flee to cities where they were held in refugee camps, Samuel Huntington called this a process of "urbanization." </P>

<P><B>THE <I>NEW York Times Magazine </I>recently ran a cover that read "The New American Empire: Get Used to It." How is that message playing in India and elsewhere outside the United States?</B></P>

<P>IN INDIA, there is a dissonance between what people think of the war and the American Empire, and the deliberately ambiguous position of the Indian government. This war against Iraq has fuelled a lot of anger among a majority of people, but there are the opportunists, among the elite in particular, who are rather stupidly hoping to be thrown some crumbs in the "reconstruction" era. They're like hyenas. Vultures. </P>
<P>No one's going to "get used" to the American Empire--no one can. This is because that empire can only survive and hold its position if it continues with its agenda of mass murder and mass dispossession.</P>
<P>These are not things people get used to, however hard they try. You can expect to be killed, but you can't get used to the idea. </P>
<P>It will be a bloody battle, this battle for the establishment and perpetuation of hegemony. The world is not a static place. It's wild and unpredictable. The American Empire isn't going to have all that easy a ride. The people of the world will not be lining the streets raining roses on the emperor.</P>

<P><B>MORE THAN 10 million people demonstrated around the world on February 15, including millions in the countries leading the war on Iraq. Why do you think we are seeing such large protests?</B></P>

<P>IT THINK that there's only one reason. America has been stripped of its mask. Its secret history of brutal interventions and unforgivable manipulations is street talk. The dots have been joined, and the outline of the beast has emerged.</P>

