<P><font face="Times New Roman, Times, serif" size="4">Pro-war hate crimes target Muslims at Yale</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Stand up to the bigots</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 18, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear Socialist Worker,</P>
</B><P>A few weeks ago, a right-wing student at Yale wrote a column entitled "The disgusting side of the antiwar movement" in which he accused antiwar activists of everything from public puking in San Francisco to being controlled by Stalinists. </P>
<P>Just a week later, we got to see the much more "disgusting" side of the pro-war movement. Shortly after the war began, Katherine Lo, a Yale undergrad, hung an upside down American flag outside her window as a symbol of distress. </P>
<P>"I'm very upset about the war our government is waging on the Iraqi people," Lo told the <I>Yale Daily News</I>. "I am upset that Bush has completely ignored public opinion of the nation and of the world."</P>
<P>A few days later, several male students, one wielding a wooden plank, entered her dorm room and attempted to break down her bedroom door. Lo, who was in the room at the time, quickly locked the door and kept quiet so they wouldn't know she was there. </P>
<P>After about 10 minutes, her assailants gave up, but not before leaving hateful anti-Muslim messages on her door. The message said: "Fuck Iraqi Saddam following fucks. I hate you, GO AMERICA."</P>
<P>News of this event has outraged the activist community, and now many are taking action in solidarity with Katherine--by displaying upside-down American flags in their windows. </P>
<P>This is not the first time such racist attacks have occurred on the Yale campus, though none had involved physical violence. Shortly after September 11, a menacing sign declaring "Kill 'em all, let God sort 'em out" was displayed out of a dorm room window. </P>
<P>Later, another Muslim student was attacked for his pro-Palestinian views in a student editorial, which read, "No Muslim individual or organization with extreme views will be safe in this country." This is in addition to students being contacted by the Joint Terrorism Task Force in regards to their political views.</P>
<P>In these times of hatred and police repression, it is all the more important that we speak out and stand up for our rights. Otherwise, we risk having them taken away altogether.</P>
<B><P>Leela Yellesetty,</B> New Haven, Conn.</P>
