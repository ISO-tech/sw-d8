<P><font face="Times New Roman, Times, serif" size="5"><b>This isn't liberation, this is conquest</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 18, 2003 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"WE'RE HERE to give you your fucking freedom," the U.S. Marine snarled at a crowd of protesting Iraqis. "Now back off." This confrontation--broadcast briefly by CNN in the early morning hours last weekend, never to be seen again--exposed the truth about the U.S. "liberation" of Baghdad.</P>
<P>The media cheerleaders declared victory last Wednesday when U.S. soldiers pulled down a statue of Saddam Hussein in Baghdad. But in a matter of days, the catastrophic consequences of the war--and the fury of its Iraqi victims--were too obvious to ignore.</P>
<P>Baghdad and other cities were gripped by looting and destruction--under the noses of U.S. soldiers. American officers ignored repeated requests to guard museums and hospitals. But they did manage to protect one government office building: the Ministry of Oil.</P>
<P>In Baghdad and Basra, hundreds of thousands of people remained without water and electricity. Almost all of Baghdad's hospitals are shut down or destroyed because of bomb damage, lack of medicine and supplies, and looting. And to add--literally--insult to injury, the U.S. Army sent soldiers into hospitals, not to help the wounded, but to carry out searches for the remnants of the Iraqi government's fighters.</P>
<P>Washington's war makers couldn't care less about the hell they've created in Iraq. Their attitude was summed up--in words they would never use publicly--by Lance Corp. Christopher Akins. Akins told a <I>Newsday</I> reporter that he hated the "ragheads." "Anybody who actively opposes the United States of America's way," Akins explained. "If a little kid actively opposes my way of life, I'd call him a raghead, too."</P>
<P>Behind all the talk about "liberation" and "freedom," this is what Washington really believes. No wonder scores of Baghdad residents took to the streets early this week in a protest at the very spot where the Saddam statue was toppled--carrying a huge banner reading "Bush=Saddam."</P>
<P>We won't let the Bush administration get away with lying about its "liberation" of Iraq. And we'll oppose the brutal and humiliating occupation that will only bring more horrors to ordinary Iraqis.</P>

