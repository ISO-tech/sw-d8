<P><font face="Times New Roman, Times, serif" size="5"><b>International antiwar movement</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 18, 2003 | Page 11</font></p>

<P><font face="Times New Roman, Times, serif" size="4"><b>Britain</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Clive Searle, Manchester Socialist Alliance </b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE BRITISH media have united to describe the stage-managed pictures of the toppling of Saddam Hussein's statue as the "defining picture of this war." But last weekend, 200,000 antiwar campaigners filled Hyde Park in London to refute those lies. </P>
<P>Thousands carried pictures of Ali Ismail Abbas, the little boy orphaned, burned and mutilated by a U.S. missile. For millions, Ali's harrowing image has become the real "defining picture" of this war of occupation. </P>
<P>Speaker after speaker in Hyde Park pledged to carry on the resistance to the war makers in Washington and London. Human rights lawyer Louise Christian--who represents three British Muslims detained in the legal black hole of Guant&aacute;namo Bay--received wild applause when she denounced Tony Blair and his cabinet as "war criminals." Lindsey German, convenor of the Stop the War Coalition, described the war as one "of the rich against the poor."</P>
<P>Despite talk of "victory," millions of British people still see through the propaganda. Tony Blair and George Bush may be claiming to have won this war, but they will have a more difficult job to win the peace--both in Iraq and at home.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Ireland</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jon Anderson, Irish socialist</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THOUSANDS OF protesters converged on Belfast Monday to protest George Bush and Tony Blair's war summit. Local opinion is divided over the war, but many people were outraged at the hypocrisy of Bush and Blair meeting in Northern Ireland so they could pose as peacemakers. </P>
<P>The roughly 2,000 protesters came from all over the North of Ireland, all major cities in the South and even Scotland to join trade union-organized rally. The mood was angry and militant. Both local politicians on the platform, Mitchel McLaughlin (Sinn Fein) and Alex Attwood (SDLP), were heckled by demonstrators, including members of their own parties, because their leaders had agreed to meet Bush. The biggest cheers were for the toughest anti-Bush speeches, from left-wing journalist Eamonn McCann and veteran civil rights activist Bernadette McAliskey. </P>
<P>Unfortunately, President Bush did not see what people thought of his war. A massive security blockade kept protesters three miles from the summit.</P>

