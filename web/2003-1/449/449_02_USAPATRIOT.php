<P><font face="Times New Roman, Times, serif" size="5"><b>Will USA PATRIOT be made permanent?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 18, 2003 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>WITH THE help of congressional Republicans, the White House is pushing to tighten the noose around our civil rights by making the USA PATRIOT Act permanent.</P>
<P>When the draconian law was passed in October 2001 after a closed-door bargaining session between congressional Democrats and Republicans, Democrats proudly claimed that they had won a "concession"--that a number of provisions would "sunset," expiring automatically in 2005. </P>
<P>But now, Sen. Orrin Hatch (R-Utah) has drafted a proposal to eliminate the sunset provision--and Congress may vote on it as early as this week, by trying to attach it to another antiterrorism bill that would allow the government to use more secret surveillance against terrorism suspects.</P>
<P>Already, USA PATRIOT allows for the indefinite detention of immigrants, secret searches of your home without notification, lowered standards for wiretaps--and "guilt by association," by labeling groups or individuals that have contact with groups that the U.S. decides are "terrorists" as terrorists themselves.</P>
<P>And many of the worst aspects of the law--such as "sneak and peek" searches and cyberspace snooping--don't "sunset" and are now permanent changes in the law. </P>
<P>Senate Minority Leader Tom Daschle (D-S.D.) says that he and other Democrats "would be very strongly opposed to any repeal" of the 2005 limit. But considering that only one Democratic senator, Russ Feingold (D-Wis.), voted against the act at the time, that sounds like hot air.</P>


