<P><font face="Times New Roman, Times, serif" size="4">Trying to silence resistance</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>UMass professor targeted by cops</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mitch Lewis</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 18, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ABOUT 50 students sat in at campus police headquarters at the University of Massachusetts Boston to protest the arrest of an African American professor.</P>
<P>Professor Tony Van Der Meer was assaulted and arrested by cops for defending a student activist who was being harassed by a military recruiter. According to students who witnessed the altercation, the recruiter told Van Der Meer, "You should be shot in the head like Martin Luther King." </P>
<P>When Van Der Meer began to respond, three campus cops tackled him, tore his jacket and arrested him. "I was beaten down, cuffed and dragged off to headquarters, where they chained me to the wall, shackled my legs and frisked me," Van Der Meer told <I>Socialist Worker</I>. "They were trying their best to humiliate me."</P>
<P>A number of students who saw the attack take place began chanting on the spot, and a campus speak-out drew more than 300 angry students, staff and faculty. But police have still refused to drop the clearly bogus charges of assaulting a police officer and resisting arrest, which carry a five-year prison sentence. "What, did professor Van Der Meer's chest lunge out and hit the policeman's fist?" an angry student asked at the speak-out.</P>
<P>The campus police chief, who said publicly that he was proud of his officers, promised to "review the procedural guidelines for arrests." "This is not about procedure, this is about racism," responded Jemadari Kamara, a colleague of Van Der Meer's. </P>
<P>Van Der Meer himself has spoken out against police brutality and racism. He and a large number of students have vowed to put up a fight until the charges are dropped. "This is reflective of the crackdown on civil liberties," said Van Der Meer. "People have to speak up; this isn't a time to be silent. And we also have to stand up and expose their lies that the war on Iraq is about freedom. It's an invasion and an occupation, and if we allow it to happen to them, it will happen to us. As Martin Luther King put it, 'Injustice anywhere is a threat to justice everywhere.'"</P>

