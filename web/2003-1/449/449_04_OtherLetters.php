<P><font face="Times New Roman, Times, serif" size="4"><b>How many Iraqis really are celebrating?</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear Socialist Worker,</P>
</B><P>Just like during the Afghanistan war, the U.S. media is going through its own orgy of joy covering the celebrations of Iraqis welcoming their new occupying army. Bush and Rumsfeld must be pleased at how this private orchestra of propagandists requires very little conducting.</P>
<P>But although every major TV station and newspaper is playing the same note, there is plenty to suggest that something is out of tune. Such as when Steve Johnson wrote a piece in the <I>Chicago Tribune</I> titled "Powerful TV image obscures details." "Few of the American news outlets emphasized how small the crowd of Iraqis around the [toppled Hussein] statue really was," reported Johnson.</P>
<P>Or, when National Public Radio's [NPR] Anne Garrels reported from Baghdad on seeing Iraqis who despised Hussein's regime crying on the street in despair at watching their city destroyed and occupied by American troops.</P>
<P>"People were very dispirited, their pride was punctured, their honor was really hurt," Garrels said. "And they became very angry with Saddam Hussein. Certainly people were beginning to talk and say, you know the Republican Guards are a bunch of puffed-up whatevers. They got insignia and uniforms and they bossed us around, but when it comes to a real fight they don't do it." This hardly strikes me as a people that wanted the U.S. army to take over the city, but more as a people that wanted its army to put up a fight.</P>
<P>Given all the hot air about the Shia people of Iraq rising up against Hussein, it is very revealing that Shiites in Baghdad told Garrels that they were proud of the resistance their brethren had put up in Basra and contrasted it to the crumbling of the Republican Guards in Baghdad. Anyone without a contract at NBC, CNN or their likes may already have sniffed from where a more determined and genuinely popular resistance to the occupation will come.</P>
<P>By the way, I heard the report from Garrels on NPR on Wednesday, and by Thursday, you couldn't find any trace of Garrels' remarks about the Shiites on NPR's Web site. Maybe her contract is not as good after all.</P>
<B><P>H&eacute;ctor Reyes, </B>Chicago</P>

