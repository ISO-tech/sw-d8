<p><a name="Immigrants"></a><font face="Times New Roman, Times, serif" size="4"><b>Immigrants under attack in Oregon</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear Socialist Worker,</P>
</B><P>Seventy-three-year-old Jerry Nothman would be the last person you would think to complain about the new Homeland Security department. Jerry, who moved to the U.S. from Germany more than 50 years ago and became a naturalized citizen, said the only subversive thing he ever did was to vote Republican.</P>
<P>Yet when he returned to the U.S. from a recent European visit, he detailed the most appalling treatment from Homeland Security agents. In an open letter to the INS in the <I>Oregonian,</I> Jerry said: "In the 50 years since I arrived in this country, I had managed to forget the early days of my childhood in Germany. What a rude awakening to find that the methods of interrogation of those days are alive and well, the only difference being that instead of being carried out by the Vaterland Sicherheitsdienst, it's now in the name of Homeland Security. What a coincidence!"</P>
<P>But Jerry was luckier than Mike (Maher) Hawash who is being detained in solitary confinement as a material witness after being arrested outside his Intel workplace in Hillsboro, Ore., on March 20. FBI agents in bulletproof vests and carrying M-16s then raided his home, waking his wife and three children.</P>
<P>No one knows why Mike is being detained, not even his attorneys, whom he was kept from for several days along with his family. Mike is an Arab American who has been a U.S. citizen for 14 years. David Fidanque, of the Oregon ACLU said: "If the Mafia were to do something like this, we would call it kidnapping, coercion and racketeering."</P>
<P>The only reason anyone can think of is that Mike and his wife gave money to the Global Relief Foundation, long before the FBI alleged that the charity had terrorist connections. </P>
<P>Civil rights in Oregon--and across the country--are under attack. We must fight all of these attacks on our rights. Indeed, freedom fries while Baghdad burns.</P>
<B><P>Paul Dean, </B>Portland, Ore.</P>


