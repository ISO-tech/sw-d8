<p><a name="Vilified"></a><font face="Times New Roman, Times, serif" size="4"><b>Vilified for backing resistance</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear Socialist Worker, </P>
</B><P>Columbia University anthropology professor Nicholas De Genova has faced a vicious right-wing assault after calling for the defeat of the U.S. government in Iraq. Since making the comments at a March 26 teach-in against the war, De Genova has received more than 1,000 death threats. He has also has been the subject of a congressional letter demanding that the university fire him.</P>
<P>The hypocrisy of these attacks are obvious. While politicians have been cheering on our government's slaughter of the people of Iraq, De Genova has been vilified for asserting the right of Iraqis to defend their homes--and for suggesting that ordinary American soldiers should see their main enemy as the administration that sends them to die in Iraq.</P>
<P>Not all of De Genova's comments at the teach-in were valuable in terms of helping the antiwar movement understand how to respond to the right-wing attack on us. He could have explained himself better, in ways that would have helped other people to understand his arguments.</P>
<P>Still, the assault on De Genova is really a right-wing attempt to red-bait antiwar protesters, and to force us to prove that we, too, "support our troops." We need to stand up to these efforts to undermine our movement and refuse to accept the "support our troops" arguments that the right wing is using to attack De Genova.</P>
<B><P>Jonah Birch, </B>New York City</P>

