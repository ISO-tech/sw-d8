<P><font face="Times New Roman, Times, serif" size="5"><b>Racist frame-up exposed in Texas</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Mike Corwin</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 18, 2003 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IN A victory over an outrageous racist frame-up, authorities in Texas are moving to reverse drug trafficking convictions of 38 men and women from the small town of Tulia.</P>
<P>At a special hearing this month, prosecutors conceded that the 46 people arrested in a 1999 drug sting--most of whom were African American--were wrongly fingered by a shady undercover narcotics officer named Tom Coleman.</P>
<P>The nightmare in Tulia began on July 23, 1999, when police rounded up dozens of the town's residents in an overnight raid. The evidence against these "drug barons?" Absolutely nothing--other than the word of Coleman, who worked alone and didn't record any of his alleged drug buys. No drugs, weapons or large sums of cash were found among any of those arrested.</P>
<P>Nevertheless, the defendants who went to trial were convicted and received jail sentences ranging from 20 to 90 years. Other defendants, seeing these harsh sentences, pleaded guilty and received shorter terms or probation. </P>
<P>Joseph Moore, a 60-year-old hog farmer accused of being the kingpin of Tulia's "drug ring," described his ordeal: "The last 45 months in prison have been hell for me. My diabetes started to act up, and I almost died in jail. I don't know if anyone can understand what it means to almost die alone, incarcerated by mistake."</P>
<P>For his part in this travesty, Coleman was named 1999's "Lawman of the Year" in Texas--then under the watch of Gov. George W. Bush. The judge who presided over this month's special hearing agreed that Coleman--who testified to his own routine use of the word "nigger"--was not a credible witness and recommended that a higher court overturn all the convictions.</P>
<P>This is a victory over one of the most blatant injustices in a "war on drugs" that is racist to the core.</P>

