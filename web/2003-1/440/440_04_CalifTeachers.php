<P><font face="Times New Roman, Times, serif" size="4">The cruel choices facing California teachers</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Upside-down priorities</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 14, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>I want to tell you about how the state and local budget crises are already affecting our schools in San Francisco--even before the real details of the cuts have been announced. </P>
<P>Our principal came to a faculty meeting last week to explain to us that all spending had to stop immediately. There are new restrictions on what will or will not be copied in the office. There is no money for field trips. The free bus program for day trips has been immediately cut. Stipends for parent workshops have evaporated, and our parenting support group, taught by a paraprofessional at our school for the last 15 years, has lost funding.</P>
<P>Our principal went to her last administrators' meeting and, because of San Francisco's expected midyear cuts of $21 million, had to literally beg the district to agree to send paper and pencils to our school for the remainder of this school year. </P>
<P>I try to imagine Bank of America or the Department of Defense calling all their employees in and telling them, "There will be no more copying this year, please hoard your scrap paper." It's absurd. </P>
<P>And we're preparing for the state cuts as well. Take one look at California Gov. Gray Davis' budget, and you'll see where the real priorities of this system lie. There's no increase in corporate taxes, but the sales tax is expected to rise to 9.5 percent. About 10 percent of the education budget--some $5 billion--is expected to be cut.</P>
<P>Meanwhile, a new death row is about to be constructed at San Quentin, and the Corrections Department will see its budget rise. These outrageous, backwards funding decisions prompted another teacher here to say, "Hey, let's just change our name to Correctional Facility. That way, we can get more money."</P>
<P>It's time that teachers, and all workers, see what this system is really about and start fighting for a system where no student has to worry about having a pencil to write with or paper to write on.</P>
<B><P>Adrienne Johnstone,</B> United Educators of San Francisco, Union Building Representative</P>


