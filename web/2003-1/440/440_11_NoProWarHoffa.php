<P><font face="Times New Roman, Times, serif" size="4">A letter from rank-and-file Teamsters</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Opposing Hoffa's support for war</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 14, 2003 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I></P>
</B><P>As rank-and-file Teamsters, we are outraged to learn that our president, James Hoffa, has joined with war hawks like Newt Gingrich and Richard Perle to form the Committee for the Liberation of Iraq.</P>
<P>We urge him to resign from this committee immediately for the following reasons.</P>
<P>The committee is a phony attempt by a handful of right-wingers to create the appearance that a broad section of Americans support war with Iraq. In fact, support for the war is dropping. Dozens of union bodies, including Local 705, the second-largest Teamster local, have passed resolutions against war.</P>
<P>The committee includes figures from the American Enterprise Institute, a right-wing think tank with an anti-union agenda. They would love to see a war divert working people's attention from the faltering economy, corporate scandals and massive tax cuts for the rich.</P>
<P>They also know that war will provide an opportunity to attack basic labor rights, as has already happened with the West Coast dockworkers and federal employees in the new Department of Homeland Security.</P>
<P>The histories of the members of this committee show they care little about liberation or democracy--in Iraq or anywhere else. George Schultz, for example, was secretary of state in the 1980s when the U.S. government was arming Saddam Hussein and other dictators. </P>
<P>No member of this group has opposed the U.S. and United Nations (UN) sanctions against Iraq. These sanctions haven't done anything to weaken Saddam Hussein, but have killed more than 500,000 Iraqi children under the age of five, according to the UN.</P>
<P>As leader of the country's largest labor union, it is Hoffa's duty to fight for the interests of Teamsters and other working people, not to support a war on another country and its people. </P>
<B><P>Joe Allen,</B> shop steward, IBT Local 705<BR>
<B>Danny Katch,</B> shop steward (alternate), IBT Local 804<BR>
<B>Sam Putinja,</B> shop steward, IBT Local 938<BR>
<B>Donny Schraffenberger,</B> shop steward, IBT Local 705</P>
