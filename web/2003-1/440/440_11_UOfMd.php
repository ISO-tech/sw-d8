<P><font face="Times New Roman, Times, serif" size="5"><b>University of Maryland</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Kathryn Weber</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 14, 2003 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>BALTIMORE--More than 35 students and workers gathered to fight the cuts that University of Maryland administrators have been laying on the backs of students and workers alike.</P>
<P>In response to the obvious divide-and-conquer tactics of the administration, both students and workers made their message plain--"the workers depend upon the students, and the students depend upon the workers."</P>
<P>For the last 10 years, campus workers, members of AFSCME Local 1072, have been fighting an uphill battle. Promotions aren't based on experience but connections, and some university workers haven't gotten a raise in more than 10 years.</P>
<P>The administration is pursuing an all-out assault--an end to tuition remission, no cost-of-living increases in wages, a freeze on promotions, no increase in health benefits and, for students, a mid-semester tuition raise. </P>
<P>Mail services workers were told there was no money available for them--days before an assistant manager position earning $50,000 was added to the department. "The administration is going to want to pacify the workers by taking it out on the students," said one worker, "but we want you to know that we don't want that." Everyone in the room agreed that "something is wrong on this campus&#133;and it's the administration."</P>


