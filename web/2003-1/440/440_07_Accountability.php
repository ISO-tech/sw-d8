<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT DO SOCIALISTS SAY?</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Are the politicians answerable to us?</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 14, 2003 | Page 7</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ON JANUARY 18, half a million people demonstrated against George W. Bush's war on Iraq in cities across the country. And this weekend, even more protesters are expected in New York City, San Francisco and elsewhere--not to mention millions more people across the world. Yet the Bush administration seems as determined as ever to go to war.</P>
<P>This poses a basic question: Does Bush have to listen to those who speak out against war? After all, he's the leader of a government supposedly based on "we the people," as the U.S. Constitution puts it. Isn't he in some way answerable to the massive opposition to the war on Iraq?</P>
<P>The short answer is no. Bush is far more accountable to his corporate backers in the oil industry than to voters or protesters.</P>
<P>"We the people" don't actually get to vote on questions like whether the U.S. goes to war. We may vote for elected officials every two, four or six years. But there's absolutely no guarantee that they'll carry out their promises--and no realistic mechanism for recalling them if they don't.</P>
<P>This is why there's a long history of politicians saying one thing to get elected, and then doing another once in office. In 1964, Lyndon Johnson ran for president as a "peace" candidate who would provide an alternative to the "belligerent" policies of Republican Barry Goldwater. Johnson's next four years in office were marked by the escalation of the U.S. war on Vietnam.</P>
<P>We live under a government that gives the appearance of popular participation, but where government officials serve the interests of the tiny minority who own and control the vast majority of the nation's wealth--even when this is at odds with the will of the majority. </P>
<P>The fact that the wealthiest nation on earth refuses to provide shelter, health care and employment to millions of its own citizens is evidence enough of the lack of true democracy in the most meaningful sense of the term. That's why socialists refer to this kind of government as "bourgeois democracy."</P>
<P>For socialists, there is no such thing as democracy in the abstract. Democracy has to be understood in terms of its class content--that is, to what extent working people exercise real decision-making power. Even the most democratic bourgeois democracy remains a tool in the hands of the minority capitalist class to maintain the status quo and keep the working class in submission.</P>
<P>This doesn't mean that socialists think democratic institutions and civil liberties are irrelevant. Obviously, in comparison with the authoritarian forms of rule that came before them, bourgeois democracy is a great advance. Karl Marx himself cut his political teeth in a battle for freedom of the press in mid-19th century Germany. In essence, he was fighting to expand liberal democracy in a state where virtually all criticism of the government was censored.</P>
<P>But while liberal reformers seek structures for democratic rights and institutions as ends in themselves, socialists fight for these reforms as a means to express popular pressure, not to contain it.</P>
<P>Mobilizing that popular pressure on a mass scale can stop the war plans of a U.S. president, as the movement against the Vietnam War proved. That struggle--plus the breakdown of the U.S. army in Vietnam and the determination of the Vietnamese liberation fighters--shook the power of the U.S. ruling class.</P>
<P>But the important point to remember is that Bush won't ever really care about what ordinary people think of his wars--unless he's forced to. As a whole, the U.S. government will always serve the interests of the small minority at the top of society, which cares, above all else, about maintaining its own power and wealth.</P>
<P>For "we the people" to ever have any real control over the social decisions that affect our lives, we have to replace a political system that gives only the appearance of popular participation--with a socialist society that is based on democracy.</P>


