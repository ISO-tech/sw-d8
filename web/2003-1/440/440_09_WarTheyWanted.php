<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Getting the war they wanted</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 14, 2003 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>OF THE hundreds of lies the Bush administration has used to justify war in Iraq, the biggest of all is the notion that the U.S. wants to avoid war. The fact is that this war has been on the drawing board for years.</P>
<P>Neo-conservative ideologues--many of whom now direct the Pentagon's and State Department's policy offices--have clamored for "regime change" in Iraq for more than a decade. But the most detailed statement of the Bush administration's current policy appeared in a little-noticed report that appeared in September 2000.</P>
<P>The report published by the Project for the New American Century (PNAC) and authored by administration figures like Deputy Defense Secretary Paul Wolfowitz, Undersecretary of State John Bolton and top vice presidential aide Lewis Libby, called for stepped-up military intervention in the Middle East.</P>
<P>"While the unresolved conflict with Iraq provides the immediate justification, the need for a substantial American force presence in the Gulf transcends the issue of Saddam Hussein," the PNAC report said. War with Iraq would be part of a plan of "maintaining global U.S. preeminence&#133; and shaping the international security order in line with U.S. principles and interests."</P>
<P>Even PNAC realized the vast expansion of imperial ambition and military power they envisioned would not be an easy sell to a public that had welcomed the end of the Cold War a decade earlier. But they predicted that their time would come in the event of "some catastrophic and catalyzing event--like a new Pearl Harbor."</P>
<P>September 11 was the opportunity they'd been waiting for. Under the cover of the "war on terrorism," the U.S. is launching an open-ended military campaign to preserve and secure U.S. dominance in the world well into this century.</P>
<P>Again, this project has been on the drawing board for years. In 1992, Wolfowitz, then a Pentagon official in Bush Sr.'s administration, authored a top-secret paper advocating U.S. dominance in the post-Cold War world. U.S. policy "must now refocus on precluding the emergence of any potential future global competitor&#133;[W]e must maintain the mechanism for deterring potential competitors from even aspiring to a larger regional or global role," the report said.</P>
<P>When word of Wolfowitz's document leaked to the press, it caused an uproar. Even U.S. allies like Germany and France found shocking the notion that the U.S. planned to "deter" them. The Pentagon was forced to distance itself from the Wolfowitz paper. But a decade later, the essence of that paper appeared in the September 2002 "National Security Strategy" document that announced the "Bush doctrine."</P>
<P>"Our forces will be strong enough to dissuade potential adversaries from pursuing a military buildup in hopes of surpassing, or equaling, the power of the U.S." the Bush doctrine proclaimed. This set off alarm bells around the world--not only in Baghdad and Pyongyang, but in Moscow and Berlin as well.</P>
<P>This might help to explain why U.S. allies in the "war on terrorism"--Germany, France, Russia and China--have been so reluctant to endorse Bush's war in Iraq. They realize that the U.S. war in Iraq isn't only about Iraq. It's also about how the U.S. plans to deal even with its "allies" in the future.</P>
<P>They know that if the U.S. grabs the world's main source of oil--on which they are more dependent than the U.S.--the U.S. can wield huge influence over their future economic and military development. Knowing they can't stop U.S. war plans, they have tried to clip Bush's wings with maneuvers at the United Nations.</P>
<P>But don't think France and Germany are allies in the fight against Bush's war. In all likelihood, they will give Bush some kind of "international" cover for his war. But the tensions between U.S. and Western allies show that Bush's plans for empire portend a more dangerous and unstable world. </P>


