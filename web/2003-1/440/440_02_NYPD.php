<P><font face="Times New Roman, Times, serif" size="4">New York cop lies about Central Park case</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Smearing the innocent</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Danny Katch</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 14, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE NEW York Police Department and their friends in the media are determined to smear the Central Park Five. And the cops won't let anything--particularly the truth--get in the way.</P>
<P>The five African American men convicted in the 1989 rape and beating of a white jogger in Central Park were exonerated by Judge Charles Tejada in December--though not until each had spent years behind bars.</P>
<P>But late last month, the cops spewed more lies after their own internal "investigation" showed that the five were "most likely" involved in the rape. This flies in the face of the evidence that the crime was committed by one man, Matias Reyes, a serial rapist who confessed in prison last year and was connected to the victim by DNA evidence.</P>
<P>But the cops couldn't care less about the facts. When a <I>New York Times</I> reporter asked about one particularly implausible scenario that appeared in the police department's report--how five people could have left a narrow trail in the grass--panelist William Armstrong replied, "Ye gods, we're not Sherlock Holmes."</P>
<P>The NYPD and the right-wing media are obscuring the truth because they've been using the Central Park Five as a racist rallying cry for a more repressive criminal justice system. At the time of the attack, then-Mayor Ed Koch called for harsher penalties for juveniles, and Donald Trump, the real-estate tycoon and all-around bonehead, spent $85,000 on full-page ads calling for reinstatement of the death penalty in New York state--for teenagers! "They should be forced to suffer," the ads said.</P>
<P>In the late 1990s, the law-and-order hysteria faded in New York after a large movement against police brutality developed, fueled by the cop torture of Abner Louima and the police shootings of unarmed men like Amadou Diallo and Patrick Dorismond.</P>
<P>But September 11 caused a setback--with New York cops hailed as heroes. In this climate, there was little protest when police shot four unarmed men in the opening days of 2003. The victory for the Central Park Five can be an important part of turning the tide. We can't let the cops get away with their lies.</P>

