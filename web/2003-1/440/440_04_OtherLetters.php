<p><a name="Mandela"></a><font face="Times New Roman, Times, serif" size="4"><b>Mandela blasts Bush's war</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>Nelson Mandela, a leading symbol for oppressed people all over the world, is criticizing Bush's war. He said recently that the only country responsible for violence and destruction in the world today is the United States. </P>
<P>Similar things were once said by Rev. Martin Luther King about the U.S. war in Vietnam. "The greatest purveyor of violence in the world today is my own government," said King.</P>
<P>Our government didn't support Martin Luther King, and they supported the racist government in South Africa. </P>
<P>Today, we are going to war against a sovereign nation in the name of liberating its people from a brutal dictator--when we ourselves support a lot of other brutal dictators who oppress their own people. </P>
<P>It's time for people to wake up and realize that George W. Bush wants to make money for himself and the oil buddies in his administration. They want poor people to die for their bloody war. </P>
<B><P>Hasan,</B> New York City</P>

