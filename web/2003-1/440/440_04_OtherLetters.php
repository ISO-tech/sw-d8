<p><a name="Money"></a><font face="Times New Roman, Times, serif" size="4"><b>Money for books, not for U.S. bombs</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Since the October 26 antiwar protests, my high school, Woodrow Wilson, had been organizing a student demonstration for the week of January 18. We were planning to rally outside our school for the entirety of the school day. We were hoping for maybe 20 students.</P>
<P>We did better than that. On January 14, almost 120 students were outside to protest in the cold weather against an unjust war. Days before the protest, key organizers were called into the principal's office in an attempt to shut down the protest. But we did not flinch, and the protest was carried out as planned.</P>
<P>This was not a regular antiwar protest, though; our demands were linked to the war budget and cuts in the schools and to an attack on our civil liberties. We included such demands as no money for war from social programs, and the repeal of the No Child Left Behind act. We carried signs reading "Money for books, not for bombs" and marched around the school, chanting, "Hell no, we won't go. We won't fight for Texaco." </P>
<P>We were facing arrest and suspension, but with the support of our teachers (led by an ISO member), we received a meek 20-minute detention--a clear victory for us. That weekend we turned out more than 100 students for the January 18 protest and several more for the January 19 demonstration. </P>
<P>Our demonstration is a clear representation of ordinary people linking their struggle to a greater one and even challenging the systems that be.</P>
<B><P>Mat Hanson,</B> Washington, D.C.</P>

