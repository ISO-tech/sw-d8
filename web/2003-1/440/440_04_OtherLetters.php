<P><font face="Times New Roman, Times, serif" size="4"><b>What the real state of the union looks like</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear<I> Socialist Worker,</P>
</B></I><P>Two days after Dubya gave his State of the Union address, student antiwar activists at Wake Forest University drafted and presented their own version of the State of the Union. </P>
<P>Bush's speech, warmly received by Republicans and Democrats alike, called for "tax relief," an end to "frivolous lawsuits" and a "renewed commitment to fighting evil." Student activists at Wake Forest pointed out that Bush's "tax relief" will mean nothing but tax breaks for America's wealthy and even more misery for America's poor. Bush's proposed plan would give, on average, $24,000 to America's wealthiest 1 percent annually--and that's just from eliminating the dividend tax.</P>
<P>We also pointed out that Bush's gripes about "frivolous lawsuits" were nothing but a smokescreen. Health care's real problems come from poor state and federal funding, not from patients or "overzealous" lawyers. Bush's tax cuts will only deepen the crisis in the health care industry. </P>
<P>Bush also used the State of the Union address to hype his horrific war on Iraq. He even had the nerve to suggest that the "civilized" world had the duty to bring democracy to Iraq. What a racist! We used our State of the Union address to point out that, to Bush and his cronies, Iraqi lives aren't worth saving. What the Washington hawks have in store for Iraq is death and destruction, not democracy. </P>
<P>We ended our address by pointing out that by giving tax breaks to the rich and prosecuting a war in Iraq, Bush is asking America's poor to finance a war against Iraq's poor, all while America's wealthy elite sit back and make a buck.</P>
<P>It is vital that we organize a strong grassroots, student-based movement against the war in Iraq, and that we expose the Washington warmongers for the hypocrites they are!</P>
<B><P>Doug Kennedy,</B> Winston-Salem, N.C.</P>

