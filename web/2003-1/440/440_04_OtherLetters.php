<p><a name="SUVs"></a><font face="Times New Roman, Times, serif" size="4"><b>I have no sympathy for drivers of SUVs</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>Contrary to <I>Socialist Worker</I> ("Are SUVs the reason for Bush's Iraq war?" January 31), I have no problem shaming sport utility vehicle owners as part of building the antiwar movement.</P>
<P>SUV owners are not passive victims of the automobile and oil industries, and SUVs are not "working-class" vehicles by and large. They are status machines for the upper-income and ruling classes, who are hardly lacking in transportation options.</P>
<P>Is the U.S. preparing to assault Iraq only to provide cheap fuel for wasteful SUV drivers in the U.S.? Of course not. Clearly there are many economic and strategic interests in play. </P>
<P>However, if hostility to pampered SUV-drivers attracts layers of activists to the antiwar cause, then great by me. Environmentally conscious SUV opponents (like the nonsmokers before them who took on the tobacco industry) are a large and growing constituency who should be welcomed into the movement for peace and social justice.</P>
<B><P>Paul Dorn,</B> San Francisco</P>


