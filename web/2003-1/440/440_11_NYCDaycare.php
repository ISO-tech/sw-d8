<P><font face="Times New Roman, Times, serif" size="5"><b>New York City day care workers</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 14, 2003 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Day care workers were set to hold a one-day strike February 12, as <I>Socialist Worker</I> went to press. The 7,000 day care workers--members of AFSCME District Council 1707--have been working without a contract since December 31, 2000.</P>
<P>For more than two years, the city has stonewalled in negotiations. "They want us to get together with the Day Care Council and come up with 'givebacks," said Glenn Huff, president of the Day Care Workers Local 205. "They are waiting for us to call them and say what we are going to give back," said Huff. "They are going to be waiting for a while because we don't have anything to give."</P>
<P>Day care workers are demanding a 4 percent raise for each year of the three-year contract as well as salary parity for day care center teachers with teachers at the Department of Education.</P>


