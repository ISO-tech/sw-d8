<P><font face="Times New Roman, Times, serif" size="4">Bush steals from the poor to give to the superrich</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Robin Hood in reverse</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 14, 2003 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>DOES ANYONE remember when George W. Bush campaigned for president claiming to be a compassionate conservative? Bush's new budget is all conservative and no compassion.</P>
<P>Huge increases in defense spending and more tax cuts for the wealthiest of the wealthy will push the federal government's deficit to $304 billion for next year--even after Washington makes huge cuts in social services and programs for the poor.</P>
<P>"We've got huge needs in national defense and homeland security that are driving a lot of the spending," House Budget Committee Chairman Jim Nussle (R-Iowa) told Reuters. "You've got to buy the groceries, but you don't have to remodel the kitchen," he quipped.</P>
<P>What arrogance! Nussle and his pals know that the massive cuts in programs for the poor will leave plenty of families without enough to "buy the groceries"--or pay the rent or keep the electricity on. They just don't care--as long as they can stuff more money in the pockets of the superrich.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>What they showered money on...</b></font></P>
<P><B>Tax cuts for the rich<BR>
</B>Bush is proposing a $670 billion package over 10 years that would eliminate dividend taxes and speed up cuts in income tax rates. The Urban-Brookings Tax Policy Center estimates that eliminating taxes on stock dividends--the most expensive part of the Bush plan--would give an extra $6 to taxpayers making less than $10,000 a year. Those making more than $1 million a year would pocket $45,098.</P>
<P>Adjusting for lost interest, the Center on Budget and Policy Priorities estimates that the Bush tax cut will cost a whopping $920 billion by 2013--with most of it going straight into the pockets of the filthy rich.</P>
<B><P>Defense<BR>
</B>The Pentagon budget for next year will be a whopping $399 billion--a 4.4 percent increase over this year's $382.2 billion. Bush is also asking for increases of $20 billion annually over the next five years--so that the Pentagon's already bloated budget will balloon to more than $500 billion.</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>What they slashed money from...</b></font></P>
<P><B>Medicare and Medicaid<BR>
</B>The Bush budget proposes spending $400 billion over the next decade to "reform" Medicare and offer a prescription drug benefit. But the administration's plan is to limit the prescription drug benefit to participants in HMOs--currently less than 15 percent of the nearly 40 million seniors who receive Medicare. </P>
<P>Bush's budget also goes after Medicaid, the federal program that provides health care to the poor, offering $12.7 billion over the next seven years to states willing to undertake "reforms." That means that the more poor people a state can cut from its rolls, the more money it would receive--a pattern that proved disastrous during welfare "reform" under the Clinton administration.</P>
<B><P>Education <BR>
</B>Bush may have called his education legislation passed last year "No Child Left Behind." But there'll be lots of children left behind by the White House's new budget. The Department of Education budget will increase, but Bush wants to eliminate 45 different federal education programs--including after-school programs, teacher training and education technology projects. And overall, the budget for K-12 education in the country's poorest schools will fall $6 billion short of the amount that the Bush administration originally promised to spend next year.</P>
<B><P>School lunches <BR>
</B>Under proposed new rules, families of the 28 million kids in the National School Lunch Program would have to produce "evidence" of their poverty--in the form of pay stubs, tax returns and welfare documents--in order for their kids to qualify to eat.</P>
<P>In one pilot project that tightened the restrictions, the number of children approved for the free lunch program was cut by 50 percent. That's both because families who just missed the poverty cutoff were forced to drop out of the program, but also because many who did qualify couldn't deal with the paperwork and increased bureaucratic hurdles.</P>
<B><P>Welfare<BR>
</B>Under the Bush plan, states would be required to dramatically increase the number of welfare recipients who must participate in work activities--and increase the required hours to 40 each week. But in a sickening twist, Bush's budget did manage to scrape up $1.2 billion in federal funds to market "marriage promotion" projects to welfare recipients.</P>


