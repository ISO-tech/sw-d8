<font face="Times New Roman, Times, serif" size="5"><b>Stop the war!</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">February 14, 2003 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH is determined to get his war on Iraq--no matter what the cost. His Pentagon is preparing an assault, nicknamed "Shock and Awe," that will leave "no safe place in Baghdad," in the ominous words of one military official.</P>
<P>And earlier this week, Bush was already preparing an alibi for the horrifying death toll that the attack will inevitably produce. "Saddam Hussein regards the Iraqi people as human shields, entirely expendable when their suffering serves his purposes," Bush said. </P>
<P>So according to Bush, any Iraqis who die will be victims of Saddam, not the U.S. government. What a sickening lie!</P>
<P>All along, the Bush gang has tried to sanitize the horrors that a war on Iraq would bring--or at least disguise the assault as a noble effort to liberate the Iraqi people from an evil dictator. But more and more people aren't buying it. Around the world, millions of people are preparing to march this weekend in New York City, San Francisco and literally hundreds of other cities.</P>
<P>But as <I>Socialist Worker</I> went to press, organizers of the New York protest had been denied a permit by a federal judge to march in front of the United Nations building. Civil liberties lawyers filed an emergency appeal, and community groups, unions, immigrant rights organizations and a range of celebrity figures organized press conferences and other events to put pressure on the city to back down.</P>
<P>The twisted irony of denying the right to protest at home, while invading Iraq in the name of democracy, is obvious. Unless you happen to be a member of the <I>New York Sun</I> editorial board. "The longer they delay in granting the protesters a permit, the less time organizers have to get their turnout organized, and the smaller the crowd is likely to be," crowed the <I>Sun</I> last week. </P>
<P>But we won't be intimidated. It's right to march against a war for oil profits that will turn the lives of literally millions of Iraqis into hell on earth. And it's right to defy the politicians and judges who want to silence our voices of resistance.</P>
<P>No war on Iraq! No attacks on civil liberties!</P>

