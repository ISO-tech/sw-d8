<P><font face="Times New Roman, Times, serif" size="5"><b>Waging the fight for dignity in the fields</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Justin Akers</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 14, 2003 | Page 9</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>BOOKS: <I>Human Cost of Labor: Farmworkers' Lives, Labor and Advocacy.</B></I> Charles Dillard Thompson and Melinda Wiggins, eds., University of Texas Press, 2002, 320 pages, $21.95.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>IN THE book <I>The Human Cost of Food,</I> farmworkers and labor advocates expose the insidious underbelly of the U.S. agriculture industry. The chapters meticulously study the human toll of farm work--low wages, lack of housing and health care, precarious working conditions and the absence of protective laws.</P>
<P>The authors are careful to frame these conditions as the result of an ongoing class war in the fields. They depict a history of struggle for justice, matched by the growers' ruthless efforts to keep unions out.</P>
<P>Today, there are an estimated 2.5 million farmworkers, 1.3 million of whom are migrant labor. About 75 percent of farmworkers live in abject poverty, making less than the minimum wage. Less than 20 percent have any access to health care services, while farm work ranks as one of the most deadly occupations in the U.S.</P>
<P>The authors explain these conditions as a result of a long history of union-busting and anti-immigrant policies in the U.S. For example, when workers gained the right to form unions through the National Labor Relations Act of 1935, farmworkers were explicitly excluded. In addition, the criminalizing of immigration has contributed to the mass disenfranchisement of farmworkers. </P>
<P>The book gives a human face to the struggle for justice through anecdotal evidence and testimonies. Farmworkers are portrayed as people with the power to change their conditions. The book, targeted at activists, implores readers to be an active participant in the fight.</P>
<P>The main weakness of the book lies in the absence of analysis of the role of the state in the oppression of farmworkers, leaving out any discussion of the Border Patrol and the Immigration and Naturalization Services and their historic role as labor cops and strikebreakers.</P>
<P>By avoiding these issues, the book fails to draw the important connection between the economic and political interests of the employers and the U.S. government's role in guaranteeing these interests are fulfilled.</P>


