<P><font face="Times New Roman, Times, serif" size="4">Movie exposes Australia's racist policies</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Following a fence to freedom</b></font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>MOVIES: <I>Rabbit-Proof Fence,</B></I> directed by Phillip Noyce, screenplay by Christine Olsen, starring Everlyn Sampi, Laura Monaghan, Tianna Sansbury and Kenneth Branagh.</P></font>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Elizabeth Lalasz</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 14, 2003 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><I><P>RABBIT-PROOF Fence</I> is a powerful film based on the true story of three aboriginal girls' fight against the racist forced assimilation policies of Australian government.</P>
<P>This film's director, Phillip Noyce, is also responsible for <I>The Quiet American,</I> the movie about the U.S. involvement in the Vietnam that studio executives kept from public release in the interest of "patriotism" after September 11.</P>
<P>From 1909 up until 1970, the Australian government had a policy of "assimilating" aboriginal people into the white population--forcibly taking children from their families to live in camps. <I>Rabbit-Proof Fence</I> tells the story of Molly, Gracie, and Daisy--children designated "half castes" by the Australian government and taken from their families in 1931.</P>
<P>They are a part of what is called the "Stolen Generation"--children forced into indentured servitude for whites for the rest of their lives. This racist policy is represented in the film by the state's Chief Protector of Aborigines A.O. Neville (played by Kenneth Branagh).</P>
<P>Neville is a racist bureaucrat with extraordinary powers over the aborigines. He decides whether couples are allowed to marry or not, even whether they receive a pair of new shoes. An amazing scene in <I>Rabbit-Proof Fence</I> reveals the depth of Neville's racism, as he describes to a group of Australians how to breed "the black" out of the "half-castes" to eliminate this unacceptable "third race."</P>
<P>The horror of these polices is made shockingly real in the gut-wrenching scene of the girls' abduction from their mother by local police. Shortly after arriving at the Moore River assimilation camp, the children are subjected to all forms of humiliation, including "skin checks" to see if they are "white enough."</P>
<P>Molly convinces the two others to escape the camp and flee for home. Initially uncertain about how to get home, they discover the rabbit-proof fence--1,500 miles of fencing that divides the country--and decide to follow it through the scorched Western Australian terrain and make their way home.</P>
<P>On their long and dangerous journey home, Molly, Gracie and Daisy outsmart police and an aboriginal tracker working against them, every step of the way. This drives Neville crazy, disproving his racist stereotype that aborigines are "Neanderthals."</P>
<P>The movie also shows that the racist policies of the Australian government weren't supported by all Australians. Throughout the girls' trek, both white farmers and aborigines help them along the way.</P>
<I><P>Rabbit-Proof Fence</I> is a moving, well-acted film that reveals the dirty secrets of Australia's racist past--crimes for which current Australian Prime Minister John Howard still refuses to apologize. And this film displays three young girls' strength and determination to fight back.</P>


