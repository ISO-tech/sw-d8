<P><font face="Times New Roman, Times, serif" size="5"><b>Putting a nuclear reactor into space</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 14, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>MAINSTREAM PAPERS have been packed with stories and analysis about the Columbia space shuttle disaster. But few have mentioned a Bush administration scheme that would have turned the Columbia disaster into a catastrophe.</P>
<P>Last October, Rocketdyne, a division of aircraft giant Boeing, won a contract to develop nuclear power and propulsion systems for space uses as part of the NASA's "nuclear systems initiative." In addition to a nuclear-powered rocket, NASA wants more plutonium-energized space probes--it's already launched several--and planetary rovers with nuclear systems.</P>
<P>In May and June, NASA is planning to launch two rockets from Florida carrying rovers to be landed on Mars equipped with heaters powered by plutonium. What happens if there's another disaster? As anti-nuclear activist Karl Grossman told the <I>San Francisco Chronicle,</I> if a nuclear reactor had been aboard the Columbia, the result would have been "a Chernobyl in the sky." </P>
<P>NASA's own "Environmental Impact Statement for the Mars Exploration Rover-2003 Project" says that "the overall chance of an accident occurring" for each launch "is about 1 in 30" and "the overall chance of any accident that releases radioactive materials to the environment is about 1 in 230." But the Bush gang seems to think that these are acceptable odds.</P>


