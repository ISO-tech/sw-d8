<P><font face="Times New Roman, Times, serif" size="5"><b>Tom DeLay's war on unions</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | February 14, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WHEN IT comes to waging war on workers at home, House Majority Leader Tom DeLay (R-Texas) figures it's time to go nuclear. In a six-page fundraising letter for the anti-labor National Right to Work Foundation, DeLay attacked union leaders for trying to "grab more power" after the September 11 attacks.</P>
<P>"It is truly sickening that, at a time when we desperately need everyone in America to pull together, the big labor bosses are willing to harm freedom-loving workers, the war effort and the economy to acquire more power," wrote DeLay.</P>
<P>His evidence? The work stoppage on the West Coast docks. But DeLay neglected to mention that the shipping bosses--not dockworkers--brought the ports to a halt when they locked workers out.</P>
<P>DeLay went on to denounce the International Association of Fire Fighters for trying to win collective bargaining rights "as the World Trade Center and Pentagon still smoldered." That's a pretty sick statement--given the fact that 343 union firefighters died in the World Trade Center.</P>


