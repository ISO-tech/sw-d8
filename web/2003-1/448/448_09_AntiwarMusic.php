<P><font face="Times New Roman, Times, serif" size="4">Musicians take on Bush's war on Iraq</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Music to our ears</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 11, 2003 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"WAR IS not the answer." Marvin Gaye said it in his classic 1971 song about the Vietnam war, "What's Going On?"</P>
<P>Today, as George W. Bush carries out a brutal war against the Iraqi people, many musicians are being moved to say the same thing, raising their voices against this war and the administration carrying it out.</P>
<P>It's a tough time to do it. Corporate radio giant Clear Channel and MTV have both warned against airing music that could be deemed "sensitive" because of the war. Dixie Chick Natalie Maines was subjected to public burnings of her CDs when she said she was "ashamed" of Bush.</P>
<P>In spite of the backlash, however, some of the biggest names in music are making musical statements against the war. British pop stars George Michael and Robbie Williams have both produced antiwar tracks, and the band Audioslave recently cut a cover version of Elvis Costello's classic "(What's So Funny About) Peace Love and Understanding."</P>
<P>Alternative band REM recently released the song "The Final Straw" on their Web site. A host of left-leaning musicians have delivered antiwar efforts, including socialist folk/punk rocker Billy Bragg's recent "The Price of Oil" and anarchist band Chumbawamba's "Jacob's Ladder"--a mellow groove with a nice horn section that the band performed at the January 18 antiwar demonstration in Washington, D.C.</P>
<P>Former Clash lead guitarist Mick Jones recently teamed up with former Generation X member Tony James to produce the single "Why Do Men Fight?" And pop-punk band Green Day's Billy Joe Armstrong has recorded "Life During Wartime," a sweet--though overly introspective--acoustic song that urges people to get active in opposing the war.</P>
<P>"But war's going on right now/and I'm not doing anything about it/Without a crowd I'm not so loud/I can't do anything by myself/ But that's just another lie," sings Armstrong.</P>
<P>Lefty punk rockers Anti-Flag and German band the DONOTS teamed up to produce a track that encourages young people to organize against the war. Available free on Anti-Flag's Web site, the title of the song is "You've Been to Our Shows, You've Sung Our Songs, Now Go to a Fucking Protest!"</P>
<P>It's a call to action, with lyrics like, "Protest against injustice, state terror/On the streets of the world for the dis-empowered/You've sung at our shows, cheered right over wrong/Now is the time to hit the streets, back up the words you've sung."</P>
<P>Michael Moore recently directed a video for hard rock band System of a Down's new song "Boom!"--which takes on war and corporate globalization. It includes footage of the February 15 protests in New York City, Paris and elsewhere.</P>
<P>Hip-hop has also been fertile ground for antiwar music. The Beastie Boys have released "In a World Gone Mad" on their Web site--a somewhat surprising song, considering that the Beastie Boys are hardly known for political statements.</P>
<P>It does a good job tying together the war abroad and the war at home with lyrics like, "Get your hand out my grandma's pocket/We need health care more than going to war/You think it's democracy they're fighting for?"</P>
<P>One of the most enjoyable songs, though, has to be Public Enemy's "Son of a Bush." Who can resist singing along with Chuck D as he calls George W. "the son of a bad man" over and over? Also check out the excellent drum-and-bass track "Bomb the World" by Michael Franti and Spearhead--who are well-known for performing at Bay Area protests.</P>
<P>Of course, protest music is nothing new. Political events inspire political music--they always have and they always will. That's why it's also worth pulling out old CDs and records to find some classic antiwar grooves.</P>
<P>Check out folk singers Phil Ochs, Pete Seeger and Bob Dylan, Marvin Gaye's classic "What's Going On?" reggae legend Jimmy Cliff's "Vietnam," and the Clash's "Call Up" and "Washington Bullets," to name just a few.</P>
<P>The list of artists and songs could go on and on. However, the final word should go to soul legend Edwin Starr, who sadly passed away on April 1 from a heart attack. Starr's song "War" became an anthem of the Vietnam antiwar movement when it was released in 1970--and it remains a powerhouse antiwar song today. "They say we must fight to keep our freedom/But Lord there's gotta be a better way/That's better than/War/What is it good for?/Absolutely nothing!"</P>


