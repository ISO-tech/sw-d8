<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">Washington's tide of pro-war propaganda hides the... </font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Savage face of conquest</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 11, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>TANK-LED columns of U.S. forces roar through Baghdad--and when they meet resistance, they shoot anything that moves, including civilians. It's a far cry from the cheers of grateful Iraqis that U.S. officials predicted when they launched their war on Iraq.</P>
<P>At the end of March, Secretary of Defense Donald Rumsfeld was under fire for failing to produce the quick victory he had promised. But by the beginning of this week, he and his Pentagon gang were back to smirking about their success--and trying on their new emperor's clothing.</P>
<P>As <I>Socialist Worker</I> went to press, senior administration officials were claiming to have killed Saddam Hussein--again--with a bunker-buster bomb. But even before that, the capture of Baghdad's main airport and several raids into the capital unleashed a wave of triumphant war propaganda from Washington, amplified by the corporate media.</P>
<P>"I do believe this city is freaking ours," one U.S. officer declared. For its theme of the day during Sunday's war coverage, CNN demanded: "Were the critics wrong?"--as if the war were already over.</P>
<P>But there is a big gap between the Pentagon's version of the assault on Baghdad and the reports of international journalists based in the city--who say that Iraqi defenders pushed back several assaults.</P>
<P>Also missing from Pentagon reports are the scale of civilian casualties inflicted by U.S. forces. <I>New York Times</I> reporter Dexter Filkins described one U.S. raid into Baghdad as full of "horror and confusion"--including the killing of six members of a family of 10 whose minibus was shot up by U.S. soldiers.</P>
<P>As one Baghdad resident told <I>Arab News</I>: "One Iraqi soldier will enter a neighborhood and fire a few shots at the fighter plane, and they will respond with a barrage of shots, killing as many as 50 civilians in the effort to get him."</P>
<P>Given its massive military might against a country already devastated by 12 years of sanctions and bombings, the U.S. has overwhelming military advantages. But whatever happens, the smug war makers in Washington have already been stunned by the level of Iraqi resistance. In his arrogance, Rumsfeld overlooked the fact that Iraqis might not welcome the overthrow of Saddam Hussein if it came through an invasion and occupation by the U.S.</P>
<P>It's unclear whether the Iraqi government is able--or willing--to risk losing its tight grip on Iraqi society by mobilizing a popular armed resistance that could spread beyond its control. For its part, the U.S. plans to win control over enough of Baghdad to declare victory. Yet even when the war is officially over, the occupation is likely to look little different.</P>
<P>Most Iraqis have no doubt about what the U.S. wants from Iraq--not liberation or democracy, but conquest. This will lead to more fighting, more conflicts--and more armed resistance to the occupiers.</P>
<P>The U.S. war is a drive for oil and empire that will further punish an already devastated country and cause the deaths of thousands and thousands more Iraqis. Large numbers of people recognize this--and their opposition to Bush's war has only been hardened by every lie and distortion.</P>
<P>These opponents of war have a job to do. We have to reach out to every person who has doubts about Bush's war, answering their questions--and winning them over to an active opposition that can challenge the war makers in Washington, whether they are dropping bombs on Iraq, carrying out its occupation or lining up new targets.</P>


