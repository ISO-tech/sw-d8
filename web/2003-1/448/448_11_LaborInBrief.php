<P><font face="Times New Roman, Times, serif" size="4"><b>New York City health care</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Michael Ware</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ALBANY, N.Y.--About 37,000 health care workers rallied April 1, to protest $2 billion in cuts to state health care programs proposed by Gov. George Pataki.  Service Employees International Union Local 1199 and the Greater New York Hospital Association organized the rally.</P>
<P>Pataki claims that deep spending cuts are needed to avoid "job-killing tax increases." The union estimates that the cuts will eliminate 22,000 jobs, in addition to the 4,000 jobs already eliminated through attrition. Meanwhile, New York City Mayor Michael Bloomberg claims that revenue shortfalls could lead to $600 million in service cuts and 15,000 layoffs. </P>
<P>The roughly $12 billion state deficit and $4 to $5 billion deficit in New York City are the product of tax cuts for the rich. Pataki and former Mayor Rudolph Giuliani showered tax breaks on their patrons during the boom. </P>
<P>Once the inevitable downturn arrived, billions in surplus revenue disappeared overnight, and now the poorest New Yorkers are expected to pay with layoffs and social service cuts, like the elimination of pre-kindergarten programs.</P>
<P>"We're not giving anything back--we have nothing to give back," AFSCME District Council (DC) 37's Executive Director Lilian Roberts said in a recent meeting. "We're fighting to stay where we are and move beyond that. Everything we fought for could disappear."</P>
<P>So far, Roberts' tough talk has not been backed up with action. Although strikes by public-sector workers are illegal in New York, striking may be the union's only hope to save jobs. Hundreds of municipal workers have already received pink slips. </P>
<P>DC 37 has called for a 5:30 p.m. rally in New York City on April 29 at City Hall Park to fight layoffs.</P>

