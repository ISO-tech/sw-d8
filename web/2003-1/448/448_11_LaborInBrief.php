<p><a name="Columbia"></a><font face="Times New Roman, Times, serif" size="4"><b>Columbia graduate students</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--Columbia University graduate students seeking union representation held a several-hundred strong rally April 3 to mark the one-year anniversary since their union election was held. Columbia refuses to count the ballots and is appealing the election to the National Labor Relations Board in the hopes that Bush appointees will scuttle union recognition.</P>
<P>The rally, organized by Graduate Employees Union/Local 2110 UAW, featured representatives from other battles for union recognition on campus, including the University of Pennsylvania, Tufts, Brown, Yale and the Pratt Institute. </P>
<P>A strong show of support from other unions at Columbia greatly energized the crowd. Represented were TWU Local 241, UAW Local 259 and SEIU Locals 693 and 1199. "Today we are sending a strong message to Columbia that all the unions here support this unionization drive," UAW Local 2110 organizer Aloke Agarwala told <I>Socialist Worker</I>. "If we have to, we will all join a general strike on campus."</P>

