<p><a name="New"></a><font face="Times New Roman, Times, serif" size="4"><b>New York City teachers against the war</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--New York City Teachers Against the War held a forum April 5 to educate teachers, students and parents about the war at home.</P>
<P>Speakers addressed the crowd of 50 on subjects like military recruitment in schools, the destruction of civil liberties, the history of the United Federation of Teachers (UFT) and the potential to build an opposition inside the UFT and pass an antiwar resolution. Teachers in the audience also shared stories of how they had been able to question the war in their classrooms and organize their coworkers through buttons, petitions and antiwar rallies.</P>
<P>With their contract expiring in May, teachers will be rallying May 3 in Albany to demand no cuts to public education. Teachers Against the War plan to march as a contingent.</P>
