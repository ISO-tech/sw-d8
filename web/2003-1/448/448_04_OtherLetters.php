<P><font face="Times New Roman, Times, serif" size="4"><b>Red-baiting only hurts the struggle</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>On March 22, I was among 2,800 antiwar demonstrators at the Westover Air Reserve Base in Chicopee, Mass. The demonstration was spirited, and the mood among demonstrators was hopeful and enthusiastic, despite a small counter-demonstration.</P>
<P>However, when a representative from International ANSWER got up to address the crowd, I was shocked to hear two friends of mine try to shout him down with boos and cries of "Stalinist!" and "ANSWER sucks!" These were antiwar demonstrators doing the shouting!</P>
<P>Fortunately, a couple of fellow activists got them to stop, and I had long discussions with them afterward about how red-baiting tactics have no place in our movement. </P>
<P>They argued that they weren't trying to red-bait, but that ANSWER's politics were "beyond the pale," because the Workers World Party, which runs ANSWER, supports a host of repressive Stalinist regimes. </P>
<P>I pointed out that they hadn't tried to shout down representatives of the Catholic Church--the same church which opposes a woman's right to have an abortion and has a long history of supporting regimes every bit as brutal and oppressive as any ANSWER's members may or may not support. Instead, they had deliberately singled out socialists and suggested they had no place in the antiwar movement.</P>
<P>There will always be political disagreements between organizations and individuals and engaging in arguments and discussions about these disagreements strengthens the movement. But red-baiting at a demonstration can only divide and weaken us.</P>
<P>Articles in <I>Socialist Worker</I> about red-baiting and the antiwar movement were really helpful in my ongoing discussions with my friends, and I was able to convince them that shouting down fellow activists at a demonstration was a mistake.</P>
<B><P>Matt Weston,</B> Northampton, Mass.</P>

