<p><a name="Where"></a><font face="Times New Roman, Times, serif" size="4"><b>Where is <I>Socialist Worker</I>'s solidarity?</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>Your article "Building a broader movement" ("What We Think" editorial, April 4) was a piece of rubbish and full of weak arguments that only aim to discourage a variety of forms of resistance to the war.</P>
<P>I encourage you to maintain solidarity, and don't mischaracterize people's motives for opposing war.</P>
<P>Political activists--including you--conduct actions every day in an isolated fashion. It's authoritarian and police-infiltrator-like statements like the ones in your editorial which make your location on the left obviously suspect.</P>
<P>Where's your solidarity?</P>
<B><P>Aaron,</B> from the Internet</P>

