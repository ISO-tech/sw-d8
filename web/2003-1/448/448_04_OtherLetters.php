<p><a name="We"></a><font face="Times New Roman, Times, serif" size="4"><b>We can't "support our troops"</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</I> </P>
</B><P>Recently a student at my university argued with me that this war is thoroughly imperialist--only moments later to conclude that he must now support our troops, no matter what the reasons are for this war. </P>
<P>Through this reasoning, he has moved from organizing against the war into the pro-war camp. In response to this kind of sentiment, many in the movement argue that we should raise the slogan "Support our troops: Bring them home."</P>
<P>But there is still a problem with this call. Although I would like to see our troops brought home, we must also realistically assess how that is possible. As in the case of Vietnam, only a soldiers' strike and opposition to conducting this war for empire can bring the troops home. </P>
<P>The self-activity of working class troops in the Middle East is crucial to the success of our movement. Furthermore, we in the antiwar movement must unapologetically stand for the defeat of U.S. imperialism. </P>
<P>A defeat for U.S. imperialism is also a victory for the American working class, which will build the confidence of American workers to take on the war at home against budget cuts, abortion rights, affirmative action, etc. </P>
<P>For that to happen, we should not favor any call for "support our troops." We should instead stand firmly against this war and say: "No more blood for Empire." </P>
<B><P>Naveen Jaganathan,</B> New Haven, Conn.</P>

