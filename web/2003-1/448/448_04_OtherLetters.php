<p><a name="Inspiration"></a><font face="Times New Roman, Times, serif" size="4"><b>Inspiration for the fight ahead</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>On March 15, while on a visit to Montreal, I was proud to participate in an antiwar demonstration of almost 200,000 people. This demonstration went totally unreported in the mainstream press--as did the February 15 demonstration in Montreal, with an almost identical number of participants.</P>
<P>No single group was dominant. A diverse, multi-ethnic crowd, including lots of youth and not least of all, a large labor contingent, all shared their opposition to the war. Most of the speeches (in Spanish and Arabic, as well as French and English) focused on the single issue of war and peace. </P>
<P>The goal of the demonstration was described by speakers: "[Canadian] Prime Minister [Jean] Chr&eacute;tien, we are proud that you have not come out for this war. Now we want you to come out against it."</P>
<P>Less than 48 hours after this demonstration (there were over 30 others throughout Canada), Chr&eacute;tien pledged that Canada would not assist the U.S. war effort. This victory has, once again, shown us that mass movements are the most effective way of stopping this war and should give heart to our own upcoming struggles in days to come.</P>
<B><P>Larry Lewis,</B> Cambridge, Mass.</P>

