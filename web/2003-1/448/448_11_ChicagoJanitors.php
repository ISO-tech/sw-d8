<P><font face="Times New Roman, Times, serif" size="4">Chicago janitors vote to approve a new contract with solid gains</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>"Only by protesting will they listen"</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Orlando Sep&uacute;lveda</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 11, 2003 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>CHICAGO--On April 6, the day their contract expired, Chicago janitors voted to accept a new contract with wage increases and improvements in their health care coverage. For the first time, the janitors--members of the Service Employees International Union (SEIU) Local 1--will also receive dental coverage.</P>
<P>The day before the tentative agreement was reached between SEIU Local 1 and the building contractors, nearly 1,500 janitors rallied in heavy rain at the Thompson Center as part of their "Justice for Janitors" campaign.</P>
<P>The contract includes a 13 percent raise over three years for suburban janitors, which will bring their starting pay to $9.25 per hour. City janitors won a 9 percent raise over the same period, which will bring their starting pay to $9.90 per hour.</P>
<P>Though the janitors had sought wage parity between city and suburban pay scales, the contract makes solid gains to that end. Both downtown and suburban janitors have family health insurance benefits, but it required a one-day strike three years ago for suburban janitors to win this benefit--and they still have no pension provisions.</P>
<P>"From that strike," says Paula Aguilar, who has been a janitor for five years, "we learned that only by coming onto the street to protest will they listen to us. Only in that way will we get better benefits."</P>
<P>The inclusion of prescription drug coverage in the new contract is also a significant improvement--one that SEIU Local 1 had set out to win when they began their contract campaign many months ago.</P>
<P>The Chicago Justice for Janitors campaign is part of a nationwide effort by janitors to improve their wages, benefits and working conditions. This year, contracts covering more than 100,000 janitors expire across the country. Chicago's new agreement is especially important because it will likely set the tone for what happens in bargaining across the country.</P>
<P>The Building Owners and Managers Association of Chicago (BOMA), which represents 90 percent of building owners and managers of the commercial buildings in downtown Chicago, had tried to dampen janitors' expectations by warning that "negotiations will be taking place in an economic climate that's weaker than three years ago." But the fact that SEIU Local 1 proved three years ago that it was serious about striking "persuaded" BOMA that the "economic climate" perhaps wasn't so horrible after all.</P>
<P>The rally and march were originally planned not only to support the janitors, but also to oppose the war on Iraq by tying together the issues of labor struggle and war. For janitor Gilberto Diaz, this was very important "because the government spends a lot of money in the war, but hardly spends anything on common people like us."</P>
<P>But after the war began, the rally's keynote speaker, AFL-CIO President John Sweeney, changed his mild antiwar stance to pro-war. And despite the fact that a majority of SEIU Local 1 opposes the war, all antiwar speakers were canceled, and a group of antiwar protesters was not allowed to join the march.</P>


