<P><font face="Times New Roman, Times, serif" size="4">Worst job outlook in 20 years</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Jobless in Bush's America</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 11, 2003 | Page 12</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>IT'S THE worst job market in two decades--and it's likely to get worse. That's the inescapable conclusion following the release of March unemployment figures, which showed a 108,000 drop in payrolls for the month--on top of 357,000 jobs eliminated in February.</P>
<P>The war-saturated media may not be paying attention, but millions of people are getting desperate in their search for a job--any job. The official 5.8 percent unemployment rate "is a scam," said Antonio, who works at a State of Illinois Department of Employment Security office in Chicago. "You know that once somebody uses up their unemployment, they're no longer on the rolls?" he told <I>Socialist Worker</I>.</P>
<P>"They're not employed, they're not unemployed. They've disappeared. So the huge mass of people that are on unemployment since last March? Their benefits are up, and they've disappeared. It's the saddest thing in the world. We can't do a thing for them. It's either us or welfare. And public aid has been cut. You have to have a family to get anything from public aid. Even food stamps only last for a while."</P>
<P>Some 5 million people want a job, but aren't actively looking. They don't show up in the statistics as unemployed--and their number has increased by 360,000 over the past year. Another 4.6 million are working part-time even though they would rather have full-time work. What's more, the number of self-employed people rose 500,000 over the past year to 9.2 million--an indication that people saw no alternative.</P>
<P>Overall, there's been a net loss of 2.1 million jobs since the recession began in March 2001--with the private-sector payroll contracting by 2.6 million, the worst decline in any recession since the Second World War, according to the Economic Policy Institute.</P>
<P>Manufacturing was hit the hardest, with payrolls contracting for the 36th month in a row. Some 2.2 million manufacturing jobs have been eliminated since July 2000. But retail was also hard hit, dropping 448,000 jobs over the last two years--the worst such performance since the early 1950s. Even temp agencies, which often do well when permanent jobs decline, cut 48,000 jobs in March, nearly 1.7 percent of their workforce.</P>
<P>"With all that is going wrong in the U.S. economy, economists are starting to suspect that the current unemployment rate of 5.8 percent--the same as six months ago--could be underestimating the true level of distress in the labor market," the <I>Wall Street Journal</I> reported.</P>
<P>The problem of unemployment isn't that the U.S. economy has scarce resources. In fact, the opposite is the case. The drive for profits in the late 1990s led to an investment in too many factories, offices, telephone networks and more--too many for capitalists to make a profit, that is.</P>
<P>To make up for the gap, employers are squeezing more work out of fewer workers. "Businesses simply aren't sufficiently profitable to fuel new hiring," Richard Yamarone, an economist at Argus Research in New York, told the <I>New York Times</I>. "Why would you when you have slipping demand, skyrocketing productivity and global overcapacity?"</P>
<P>Rather than shovel money at the rich through tax cuts, Congress should be extending--and increasing--unemployment benefits. It's time to organize to demand a government jobs programs to help workers make a decent living--now.</P>


