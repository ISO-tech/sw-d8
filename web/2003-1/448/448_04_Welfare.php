<P><font face="Times New Roman, Times, serif" size="4">Politicians' dismal failure in addressing poverty</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Welfare "reform" hell</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 11, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>I recently attended a local conference celebrating International Women's Day. One workshop entitled "Welfare Reform Issues" gave a very informative, detailed and horrifying glimpse into the realities of one of our shredded social safety nets.</P>
<P>It will come as no surprise to <I>SW</I> readers that "welfare reform" is a dismal failure in addressing poverty. To begin with, no state's welfare benefits bring any recipient up to the federal poverty line.</P>
<P>There has been a 35 percent decline in all income of female-headed households since "welfare reform," and three-quarters of those who left welfare can't find jobs. Only 10 to 15 percent are gainfully employed nationally.</P>
<P>In New York, the humiliating application process is meant to discourage people from applying in the first place. All applicants are finger-imaged after filling out a seven-page application and providing 20 separate eligibility requirements. </P>
<P>If the applicant is one of the 28 percent of those accepted onto the rolls (down from 61 percent seven years ago), she must prove that she has been to 10 job interviews a week. The potential employer must sign a sheet she presents, testifying to this. How likely it is that she will be hired under such humiliating circumstances?</P>
<P>And "family values"? An entire family's benefits can be sanctioned for up to 30 days in 36 states. This can happen if the applicant misses one of her job appointments, or if her child is sick and misses too many days of school, or even if her landlord says that she paid her rent late!</P>
<P>And here is the Bush gang's new proposal for "helping" women off welfare--marriage! This marriage "promotion" initiative implies that it's okay to put women and children at risk of domestic abuse and economic coercion. And that marriage itself can solve poverty.</P>
<P>Someone at the workshop mentioned that poor people and women need to organize. This is true. But given that two out of three Americans live paycheck-to-paycheck in a sinking economy with the bosses on the offensive, it is a fight the entire working class needs to take up.</P>
<P>All workers must challenge this war on women, children, minorities, the poor and immigrants--and fight to reestablish a true safety net for all citizens as a basic human right.</P>
<B><P>Joy Finucane,</B> Rochester, N.Y.</P>

