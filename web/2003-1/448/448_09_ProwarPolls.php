<P><font face="Arial, Helvetica, sans-serif" size="2"><b>READING BETWEEN THE LINES</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Reading between the pro-war polls</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lance Selfa</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 11, 2003 | Page 9</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>OPINION POLLS show that about 70 percent of Americans supported the war in Iraq two weeks after it began.</P>
<P>Yet, only six days before Bush launched the war, a <I>Newsweek</I> poll showed that 53 percent of adults surveyed said they believed the Bush administration should "take more time to try to achieve our goals in Iraq without using military force." Only 43 percent said they wanted Bush to go to war right away--a drop from 46 percent who called for war in a <I>Newsweek</I> poll in February.</P>
<P>What happened? Did the U.S. produce a "smoking gun" that convinced about 65 million Americans to support the war? Hardly.</P>
<P>If anything, the surge of support for the war represents resignation to the war, rather than enthusiastic support. It also reflects the orchestrated pressure from the government, the political parties and the media to bludgeon people into accepting the war as a fait accompli (if I may use a French expression) or be so gauche as to "support the troops." These two pressures combine to form the "rally round the flag" effect that accompanies nearly every major U.S. war.</P>
<P>Still, the surge in support for this war is less than what George Bush Sr. felt in the 1991 Gulf War, according to Gallup and other pollsters. On average, one in 10 fewer Americans support this war compared to the 1991 war. Among some Americans, the difference is even more dramatic.</P>
<P>For instance, only 29 percent of African Americans support this war in Iraq, compared to 59 percent of Blacks who supported the 1991 war, according to Gallup. The fact that Blacks represent 12.7 percent of the population but 22 percent of enlistees in the military might explain their high level of opposition.</P>
<P>If millions of people had doubts about the war before it started, it's unlikely that these doubts will just disappear when the shooting starts. The <I>Newsweek</I> poll showed that large majorities of Americans (most by levels of 65 percent or more) believed that going to war with Iraq would cause serious conflicts with U.S. allies, increase the likelihood of terrorist attacks against Americans, cause serious problems for the U.S. in the Arab world and spur Iraq to use weapons of mass destruction.</P>
<P>In other words, even while most Americans tell pollsters they support the war, they are extremely apprehensive of what it will unleash. In fact, a Pew Center tracking poll showed that the percentage of Americans calling the war the "wrong decision" increased from 22 percent to 25 percent in the first 10 days of the war.</P>
<P>At about the same time, a <I>Time</I>/CNN poll concluded, "Perhaps surprising to many abroad, a plurality of Americans would not support a war in which 5,000 Iraqi civilians were to die. In that event, opposition to the war rises to 47 percent, against 40 percent in support. Even 1,000 Iraqi civilian deaths is too high a price to pay for many Americans, with just 50 percent willing to support such a war and 39 percent opposed under those circumstances."</P>
<P>This explains why the Pentagon is so fixated not only on winning the war quickly, but on managing the perceptions of the war in the U.S. There are indications that already the number of Iraqi civilian casualties has exceeded 1,000 and agencies like the Red Cross/Red Crescent have criticized the U.S. for civilian deaths. Yet you're not likely to hear any of this from the gung-ho "embedded" journalists or the cable TV warmongers.</P>
<P>If Americans heard the truth about the war, their support for it would plummet. That should be greater motivation for the antiwar movement to keep up its efforts to reach people who can draw these conclusions.</P>
<P>Despite the barrage of war propaganda from the media and all the pressure to cave into pro-war sentiment, one-quarter of adults still oppose the war. That represents about 53 million people--or about 3 million more than voted for Bush in 2000.</P>


