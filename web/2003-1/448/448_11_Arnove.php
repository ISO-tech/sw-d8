<P><font face="Times New Roman, Times, serif" size="5"><b>The alternative to Bush and Blair</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 11, 2003 | Page 11</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>ANTHONY ARNOVE, editor of <I>Iraq Under Siege,</I> recently returned from an antiwar speaking tour in England and Scotland. He traveled there with Michael Letwin, co-convenor of New York City Labor Against War and a founding member of U.S. Labor Against War.</P>
<P>Together, they spoke at more than a dozen meetings about the U.S. movement against Bush's war on Iraq, which is being aggressively backed by British Prime Minister Tony Blair. Arnove spoke with <I>Socialist Worker</I> about the tour and the state of the British antiwar movement.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<P>PEOPLE WERE thrilled to meet activists from the U.S., particularly because the media in Britain has done so little to report on the significant opposition that has been organized in the U.S. against Bush and against this war.</P>
<P>And they were thrilled to challenge the myth that critics of the assault on Iraq are "anti-American." British antiwar activists are not anti-American. They simply oppose the policy of the U.S. government and the fact that their own government is backing that policy, despite popular opposition.</P>
<P>On February 15, 2 million people demonstrated on the streets of London in what was probably the largest protest in British history. The Stop the War Coalition has organized antiwar committees around the country. People are openly speaking about replacing Blair as the leader of the Labour Party, saying that Labour looks more and more like the Tories every day.</P>
<P>It's exciting to see all of the antiwar forces working together in one broad coalition that has mobilized people from all walks of life. </P>
<P>In Britain, organized labor has also taken a very clear position against the war. For example, UNISON, which is the largest union in Britain, has endorsed the Stop the War Coalition. The firefighters have raised a popular slogan: Blair should be spending money to put out fires at home rather than starting fires in Iraq and the Middle East.</P>
<P>Unlike in the U.S. where the antiwar movement has been almost totally neglected by the press, we were interviewed by the BBC, Sky Television, Britain's <I>Observer </I>newspaper and radio stations in Dublin, Belfast and Edinburgh.</P>
<P>Toward the end of our trip, the <I>Guardian</I> ran a front-page photo of an American marine who had written on his helmet, "Kill 'em all." I suspect that photo didn't run on the front page of any U.S. papers.</P>
<P>The tour really gave a sense of the importance of creating these concrete international links and building greater solidarity. This is especially critical because we know that for Bush and Blair, this is only a stepping stone to future wars.</P>


