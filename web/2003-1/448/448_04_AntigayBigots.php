<P><font face="Times New Roman, Times, serif" size="5"><b>Why we have to stand up to anti-gay bigots</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 11, 2003 | Page 4</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>On the evening of March 24, I responded to a call informing me that one of my comrades had gotten his glasses broken when announcing an antiwar sit-in. Apparently, a pro-war bigot smacked him in the face.</P>
<P>When I got to the scene, I started taking pictures. On one side was the assailant, with a police officer standing next to him. On the other side was my comrade and two female witnesses, with another police officer asking them questions. In between was a group of three male bystanders. </P>
<P>After I took the first few shots of film, the three male bystanders told me to stop taking pictures, and began calling me a "stupid faggot" and a "fucking queer." Shocked and filled with an immense flood of anger, I rushed toward them and demanded that they not address me with such hateful and oppressive language. But they kept taunting: "What? The fucking queer doesn't like being called a fucking queer?"</P>
<P>All the while, the police officer that had come between myself and the three bystanders could only muster to the homophobes: "Now, now, boys, calm down."</P>
<P>I am gay, out and proud of who I am. But even if I wasn't, there is no place in this world for the type of ignorance I faced that day to exist or be tolerated without a challenge. </P>
<P>The same cops that prevented students at the sit-in from walking up the steps of their school's administration building are the same cops that allowed for this onslaught of hate speech and verbal harassment to pass without repercussions.</P>
<P>Incidents like these send out the message that it is perfectly fine to attack others based on their sexual orientation. We have to stand up to these attacks. United, we can end oppression of all forms. Gay, straight, Black, white--same struggle, same fight!</P>
<B><P>Eric Hernandez, </B>San Francisco</P>

