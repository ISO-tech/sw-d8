<P><font face="Times New Roman, Times, serif" size="5"><b>Defend affirmative action</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 11, 2003 | Page 11</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WASHINGTON--As many as 50,000 protesters rallied at the Supreme Court on April 1 to send a message to George W. Bush: "We demand affirmative action!"</P>
<P>The rally was called to coincide with the opening day of arguments in the Supreme Court case challenging affirmative action admission policies at the University of Michigan. Right-wingers in the Bush administration and beyond are hoping to use this case to deal a deathblow to affirmative action.</P>
<P>The rally began on the evening of March 31, as thousands of students gathered on the steps of the Supreme Court for an all-night vigil. The next day, the rally swelled as tens of thousands more protesters joined the throng. "I couldn't even see where the speakers were, it was so packed," one protester told <I>Socialist Worker</I>. </P>
<P>Dozens of unions, church groups, student groups, civil rights organizations and even city councils and school boards traveled to Washington for the march. While attorneys inside the court argued, the largely Black crowd outside chanted, "They say Jim Crow, we say hell no!" and, "Two, four, six, eight, we don't want to segregate!"</P>
<P>Protesters were quick to point out that the filthy rich--like George W. Bush who was a C-student at Yale University--have always had their own brand of special privileges. "The reason we need special treatment is because we had special mistreatment," Rev. Al Sharpton told the crowd. "It was against the law for us to go to school. It was against the law for us to read and write. We're not asking the court for favors. We're asking the court to make right what it made wrong."</P>


