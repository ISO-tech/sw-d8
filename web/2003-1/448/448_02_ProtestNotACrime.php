<P><font face="Times New Roman, Times, serif" size="5"><b>Making protest a crime</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Justin Ward</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 11, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>LEGISLATORS IN Oregon will be voting on a bill that expands the definition of terrorism to include forms of political protest. Under the proposal, sponsored by Republican state Sen. John Minnis, a person would commit an "act of terror" if they knowingly plan, carry out or otherwise aid in activities that disrupt transportation, commerce or the affairs of government institutions, such as schools or other facilities. Those convicted would get a minimum sentence of 25 years in jail without the possibility of parole.</P>
<P>This is nothing more than an attempt to intimidate political activists. Minnis admits that his bill is a response to massive demonstrations in Portland and Eugene.</P>
<P>The bill has come under fire from activists, civil liberties groups and unions, which rightly point out that the legislation's vague language would even threaten workers' right to strike. Pushed onto the defensive, Minnis said his original draft sounded "draconian"--but that amendments would make it "work."</P>
<P>With high-ranking state officials opposing the proposal, it probably won't become law. But Minnis' police-state legislation is another step toward the criminalization of protest started by the USA PATRIOT Act.</P>


