<P><font face="Times New Roman, Times, serif" size="4">Racist attacks target Arabs and Muslims</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>War casualties at home</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 11, 2003 | Page 2</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>THE CASUALTIES of the U.S. war on Iraq don't live only in Iraq. Since the start of the war, Arabs and Muslims across the country have been the victims in a rash of racist attacks.</P>
<P>In one of the worst, Abdullah Naderi, an Afghan businessman, was set on fire on March 25 after two men broke into his Indianapolis restaurant after closing. Investigators found gasoline cans, a crow bar, rope, aerosol cans and a disposable lighter inside the restaurant--yet still say they aren't "sure" if the attack is a hate crime. Naderi suffered burns over 60 percent of his body--and is still in intensive care. </P>
<P>In Burbank, Ill., an explosive device was thrown at the van of a Palestinian family March 22. The van's door was blown off, shattered glass was thrown up to 30 feet away, and a hole was punched in the vehicle's floor. The man arrested for the crime was convicted last year of throwing a brick through the window of a mosque. </P>
<P>In California, four Muslim women visiting a restaurant were threatened by a man who made references to raping Muslim women. </P>
<P>At the University of Wisconsin-Milwaukee, someone scrawled "Suicide bomb yourselves" in permanent marker on a display board at the Muslim Student Association's offices. And physical assaults against Muslims have been reported in Northern California, Southern California, Georgia, New Jersey and South Carolina. </P>
<P>In Yorba Linda, Calif., a Muslim teenager was badly beaten by a group that allegedly included white supremacists. </P>
<P>With Arabs and Muslims the target of a federal government witch-hunt, it's no wonder that attacks like these are on the rise. As Laila Al-Qatami, a spokeswoman for the American-Arab Anti-Discrimination Committee, told the Associated Press: "The longer the war goes on and the higher the number of American casualties [in Iraq], I think probably you can make a correlation there would be more hate crimes and harassment against Arab-Americans."</P>


