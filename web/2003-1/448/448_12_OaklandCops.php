<font face="Times New Roman, Times, serif" size="5"><b>Oakland cops' brutal attack</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Todd Chretien</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | April 11, 2003 | Page 12</font></P>

<P><font face="Times New Roman, Times, serif" size="3">POLICE IN Oakland, Calif. shot wooden bullets and tossed concussion grenades at peaceful antiwar protesters and union dockworkers, injuring several people, in an April 7 confrontation.</P>
<P>Some 500 activists had set up a picket on Oakland docks--at the terminals of American Presidential Lines, a military cargo shipper, and Stevedoring Services of America, which just won a $4.8 million contract with the U.S. government to run the docks in Umm Qasr after the Iraq war. Representatives of the International Longshore and Warehouse Union (ILWU) decided that they wouldn't cross the picket line because the police presence represented a threat to their members' health and safety, thereby shutting down the morning shift on the docks.</P>
<P>This decision proved correct. Minutes later, cops charged motorcycles into peaceful picketers and union members, threw concussion grenades at them--and then opened fire with high-velocity wooden blocks.</P>
<P>As Sue Sandlin, former co-chair of the Port Workers Solidarity Committee, described the scene, "The police came down the street toward us in a military formation. Protesters had actually decided to take down the picket because our plan was to have a peaceful protest on public property. We were trying to figure out how to disperse because the cops had blocked the entrance that most of us had used to get to the gates of the docks. We began walking away from the police lines, trying to get to the sides of the road.</P>
<P>"Then police opened fire. People were being shot in the back as they tried to get away. My friend and I dove into a ditch, and she was hit by a rubber bullet. There were people dropping after they were hit, and there were explosions all around from the police percussion grenades. I was afraid that people were being killed. The police were firing indiscriminately at protesters who were fleeing as well as workers who were stopped in their cars in the middle of the street."</P>
<P>Sandlin said the attack "was one of the most terrifying experiences I've ever had. After the initial shock wore off, everyone was appalled by the police and concerned for the people who'd been wounded. So we tended to the people who had fallen, and then we regrouped in an effort to get out as safely as possible in order to regroup later."</P>
<P>Trent Willis, an ILWU business agent told the Associated Press, "They shot my guys. We're not going to work today. The cops had no reason to open up on them."</P>
<P>Police arrested 24 protesters, including ILWU Local 10 business agent Jack Heyman. As Heyman was dragged away in handcuffs, wearing his ILWU jacket, he shouted, "I'm a union official. I have the right to represent my members!"</P>
<P>Regina, a member of the International Socialist Organization, was hit by one of the police projectiles.  "After the cops charged us, I dove into a ditch and that's when they shot me in the elbow," she said. "Some fucking democracy--we can't even have a peaceful picket!"</P>
<P>Steve Stallone, an ILWU spokesperson who was present at the protest, told <I>Socialist Worker, </I>"Clearly this was an inappropriate use of force. They police shot the union workers they were supposedly there to protect. If they'd wanted to keep people away from the docks, they could have just put up a barricade a half mile away. They never needed to shoot at what was clearly a nonviolent action."</P>
<P>In fact, the police response wasn't a spontaneous overreaction. For at least 15 minutes, the cops formed lines, took aim at fleeing protesters and fired. They came prepared to step up their violence in order to intimidate the antiwar movement and the union--which fought a bitter contract battle last year against employers and the Bush administration's intervention behind the bosses.</P>
<P>The labor movement should show its solidarity with ILWU members--and stand up for the right of antiwar activists to conduct peaceful protests by denouncing this unprovoked police brutality in clear terms.</P>

