<font face="Times New Roman, Times, serif" size="5"><b>The horror they won't show</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">April 11, 2003 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>BAKHAT HASSAN'S father had worn his best suit for the ride toward the U.S. military's lines around the Iraqi town of Karbala. "To look American," Bakhat said.</P>
<P>Those were the clothes he died in. Bakhat's parents, his two daughters, one son, two brothers, their wives and two nieces were killed when U.S. soldiers opened fire on their van as it drove toward a checkpoint.</P>
<P>"My girls--I watched their heads come off their bodies," Hassan's pregnant wife Lamea numbly told an Australian reporter from her hospital bed. "It would be better not to have the baby. Our lives are over."</P>
<P>The extended family of 17 had crammed into a van to travel to Karbala, thinking that a leaflet dropped by a U.S. helicopter said that they would "be safe" there. According to Bakhat's account in Australia's <I>Sydney Morning Herald,</I> as their van approached a checkpoint south of Karbala, his family waved to U.S. soldiers. And the soldiers opened fire.</P>
<P>"We had hope," Bakhat said. "But then you Americans came to bring us democracy, and our hope ended."</P>
<P>This is one of many horror stories in Iraq today. And they are being hidden from us. Hidden by politicians and Pentagon brass peddling their lies about a war to "liberate" the Iraqi people. Hidden by a corporate media that might as well be run by the state, so willingly does it repeat the military's propaganda.</P>
<P>At least 61 dead in the region of Hilla, 50 miles south of Baghdad--and the victims have the numerous "vicious and deep wounds" characteristic of cluster bombs, according to veteran journalist Robert Fisk of Britain's <I>Independent</I>. But the Pentagon insists there are "no indications" cluster bombs were used near Hilla.</P>
<P>At least 62 killed in the bombing of the Shu'ale market in Baghdad, a piece of the casing from a missile made by leading U.S. military contractor Raytheon found at the scene. But U.S. and British officials claim that the Iraqis bombed the market themselves--and planted the missile casing. </P>
<P>Is there nothing that these monsters won't say to excuse their war?</P>
<P>No one knows how many thousands of Iraqis are already dead, but "the number of casualties in Baghdad is so high that hospitals have stopped counting the number of people treated," according to a Reuters report quoting the Red Cross. And literally millions of Iraqis are at risk of disease and starvation because of the U.S.-British siege of Baghdad and Basra.</P>
<P>People all over the U.S. and around the world are saying no to this horror. We have been right to speak the truth about this slaughter--carried out in the name of "peace" and "liberation." And we'll continue sending our message to the war criminals in Washington: No war for oil and empire!</P>


