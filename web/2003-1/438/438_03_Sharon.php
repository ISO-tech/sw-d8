<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Bloody cost of Sharon's victory</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 31, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>AS <I>SOCIALIST Worker</I> went to press, elections in Israel were set to hand the largest number of seats in the country's parliament to the right-wing Likud Party. Despite financial scandals swirling around party leader and Prime Minister Ariel Sharon, Likud was predicted to take around 30 seats in the 120-member Knesset.</P>
<P>Palestinians in the Occupied Territories of Gaza and the West Bank will have paid for this Sharon victory with blood. During the final leg of the campaign, Sharon ordered the deepest assault on Gaza since the beginning of the Palestinian Intifada, or uprising, more than two years ago.</P>
<P>Some 50 tanks and armored vehicles, backed by Apache helicopter gunships, battered their way into central Gaza City, after raiding the town of Beit Hanoun a day before. In all, Israeli forces killed 12 Palestinians and injured 51.</P>
<P>Since his election in February 2001, Sharon has escalated Israel's war on Palestinians to unbearable levels, using targeted assassinations of Palestinian leaders and assaults on Palestinian cities to ratchet up the violence whenever a truce threatened to break out.</P>
<P>"Peace for the Israeli voter does not necessarily mean a peace agreement, but rather the lack of violence, the lack of fighting and terror," wrote Nahum Barnea, a columnist in Israel's <I>Yedioth Aharonoth</I> newspaper. "What Sharon wants is the public in Israel and the world to think that he is striving for a political settlement with 'painful concessions.' I once asked Sharon, 'Painful for who?' and he answered, 'Painful for the Palestinians.'"</P>
<P>Palestinian Authority (PA) officials rejoiced when Amram Mitzna won the December primary of the Labor Party--the other major Israeli party after Likud--considering him a "dove" who would pose a challenge to Sharon. But with the Labor Party set to take less than 20 seats--losing about one-third of their representation--the PA leadership is in despair.</P>
<P>The truth is that Mitzna, a former general like Sharon, doesn't represent a significant alternative to Sharon. He wants to return to the Oslo "peace" process--under which the PA was forced to make concession after concession while a Labor government under former Prime Minister Ehud Barak greatly accelerated the construction of illegal Jewish settlements in the Occupied Territories.</P>
<P>Up until last November, the Labor Party was part of a national unity government with Sharon's Likud. After spending nearly two years providing a fig leaf for Sharon's assault on Palestinians, Labor doesn't appear to Israelis to be a serious alternative.</P>
<P>Mitzna tried to underline Labor's few small differences with Likud by pledging not to rejoin a national unity government under Sharon. That would leave Sharon trying to build a narrow, right-wing government in coalition with ultra-nationalist religious parties who want to assassinate Yasser Arafat and carry out the mass expulsion of Palestinians. This would only increase the conflict with Israel's protectors in Washington--the Bush administration, which wants to keep Israel on a leash while it carries out its Iraq war.</P>
<P>No one should be fooled by rhetoric from the likes of Secretary of State Colin Powell, who declared at the World Economic Forum in Davos, Switzerland, that he favors a "real" Palestinian state. Palestinians have been hearing that rhetoric since the beginning of the Oslo peace process.</P>
<P>When push comes to shove, the U.S. will back Israel, its watchdog in the Middle East. We have to support the Palestinians' struggle for justice against Israel's war of terror.</P>


