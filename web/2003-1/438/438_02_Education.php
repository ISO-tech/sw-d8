<P><font face="Times New Roman, Times, serif" size="4">One year after Washington's Leave No Child Behind Act...</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>They left our schools behind</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jeff Bale</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 31, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ON JANUARY 8, George W. Bush marked the first anniversary of his "Leave No Child Behind" education act. But it's the new money for schools that's been left behind.</P>
<P>In order to pay for his tax cut giveaway to the superrich and the skyrocketing Pentagon budget, the White House is slashing spending throughout the federal budget--including extra money for education that Bush talked about with such fanfare when he signed Leave No Child Behind.</P>
<P>Democrats led by Sen. Ted Kennedy (D-Mass.), as well as education reform groups and the country's two main teachers' unions, ripped Bush and the Republicans--for their hypocrisy of claiming to care about public education while not even putting up the money for programs that they proposed.</P>
<P>But unfortunately, few of Bush's critics are spotlighting the deeper problem--that the Leave No Child Behind act itself amounts to an all-out assault on public education. The legislation picks up on the test-crazy trend of the 1990s, imposing a series of "standards" on schools and forcing each state to test every child from grades three to eight. Any school that can't make the grade is put on "the list."</P>
<P>After five years of not meeting testing cutoffs, the bill requires that the "failing" school fire all its staff, go "charter," be privatized or be handed over to the state education department. Five years may seem like a long time, but the fact is that it is virtually impossible not to be targeted as a failing school. This is because test scores, according to the new law, must be broken down along any number of lines--gender, race or ethnicity of the student, whether they receive bilingual or special education instruction, etc. All told, there are some 70 different categories of test scores to be examined, and if a school fails to show annual growth in any one of them, they are labeled as failing.</P>
<P>Among other provisions, the bill forces all teachers' aides to have at least an associates' degree, but offers no financial assistance to get the education. Another measure in the legislation requires secondary schools to hand over all vital information on their students--not to the Department of Education or the state university admissions office, but to the Pentagon! That's why a student at one antiwar rally at a high school in Washington, D.C., carried a sign that read: "You can leave ME behind! No War in Iraq!"</P>


