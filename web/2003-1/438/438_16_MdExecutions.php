<P><font face="Times New Roman, Times, serif" size="5"><b>No executions in Maryland!</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Michael Stark</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 31, 2003 | Page 16</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>"CONSIDER THE moratorium lifted." With those smug words, Maryland Gov. Robert Ehrlich's spokesperson declared that the state's machinery of death was being restarted.</P>
<P>Ehrlich, a Republican who won a narrow election for governor last November, wasted no time after taking office in lifting the halt on executions imposed last May by his predecessor, Parris Glendening--sending a green light to Baltimore County prosecutors to seek a death warrant against Steven Oken. Prosecutors are eager to use Oken's case to rebuild support for the state's capital punishment system.</P>
<P>Over the last two years, Maryland's death penalty has been under close scrutiny--first with the commutation of Eugene Colvin-El's sentence in 2000, the declaration of the moratorium, and finally, the University of Maryland's death penalty study that showed the depth of racism on death row.</P>
<P>Oken is a middle-class white man accused of killing a white victim in a string of brutal murders in 1991. He is an exception on a death row dominated by poor Blacks, and prosecutors hope that killing him will prove that the death penalty is fair. </P>
<P>But even this exception shows many of the injustices of the death penalty. As soon as Oken was arrested in Maine, and long before any of the facts of his case were known, Baltimore County prosecutors jumped in front of the TV cameras and began their clamor for an execution.</P>
<P>Oken was given a sentence of life without parole in Maine. But Maryland officials maneuvered to ensure that he was extradited, put on trial in their state and given a death sentence. To ensure that the jury voted for death, prosecutors trotted out psychological "experts" who claimed that Oken suffered from an incurable mental disease and represented a continued threat to the public--ignoring the fact that he would never walk the streets again. </P>
<P>Then there's Baltimore County's own shameless record in using the death penalty. The affluent, mostly white county is the state's leader in executions, and accounts for nine of the 12 men currently on death row. Almost all of them are poor and Black. </P>
<P>The last man executed in Maryland, Tyrone X Gilliam, who was put to death in 1998, was convicted in Baltimore County. Prosecutors built their case mainly on Tyrone's "confession"--which he gave after being taken from a hospital bed, where he was suffering from massive head injuries and, according to police reports, didn't know who or where he was. </P>
<P>The two prisoners taken off death row in Maryland since reinstatement of the death penalty--Kirk Bloodsworth, who was exonerated and freed in 1993, and Eugene Colvin-El, whose sentence was commuted in 2000 because of lack of evidence--were also victims of Baltimore County prosecutors.</P>
<P>Anti-death penalty activists are fighting to stop Ehrlich from executing Steven Oken--and to get Maryland's moratorium on executions restored.</P>

