<P><font face="Times New Roman, Times, serif" size="4">State of the Union details assault on working people</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Bush's one-sided class war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 31, 2003 | Page 16</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH'S handlers hope that they have come up with a strategy to prop up their boss's plunging approval ratings. As <I>Socialist Worker</I> went to press a day before the State of the Union address, leaks from the White House indicated that the hour-long speech would be divided in two.</P>
<P>Bush would spend exactly half his time ratcheting up the hysteria against Saddam Hussein and the "axis of evil" abroad. And he would devote the other half to "the great challenges" on the home front, aides said.</P>
<P>Bush apparently hopes to convince the public that he spends half his time devising economic policies aimed at helping "people living from paycheck to paycheck." But it isn't too hard to see that the only people who will benefit from Bush's economic plan are those who live off their stock holdings. The administration's proposal on the economy is packed with more massive tax breaks for the wealthiest 10 percent of taxpayers--leaving everyone else to fight over the crumbs.</P>
<P>Only 12 percent of those interviewed for a recent Gallup poll said Bush's tax breaks would make a big difference in their family's finances. Even this figure is a bit generous. The Urban-Brookings Tax Policy Center estimates that eliminating taxes on stock dividends--the most expensive part of the Bush plan--would give an extra $6 to taxpayers making less than $10,000 a year. Those making more than $1 million a year would pocket $45,098.</P>
<P>If Bush accelerates the income tax cuts scheduled for 2006, as he plans, the top 5 percent of taxpayers would reap 70 percent of the benefits, while the bottom 80 percent would get just 6.5 percent.</P>
<P>Bush also plans to devote a portion of the State of the Union address to the health care crisis sweeping the U.S. But instead of targeting the health care and drug companies raking in astronomical profits while 43 million people go without health insurance, Bush blames patients who sue for malpractice.</P>
<P>"Frivolous lawsuits," he claims, must be curbed to slow down the rise in health care costs--so he is calling on Congress to cap "pain and suffering" damage awards for patients. "Want to sue for $2 million? Why not add a zero and make it $20 million?" clucked Bush's supporters at the <I>Wall Street Journal</I>.</P>
<P>Such caricatures don't address the growing number of lives being destroyed by cuts in hospital staffing. A recent <I>New England Journal of Medicine</I> report estimated that 1,500 patients a year in the U.S. end up with a foreign object, such as a scalpel or sponge, sewn into their bodies after surgery.</P>
<P>In an effort to further privatize the Medicare program, Bush also has a blackmail offer for the elderly--parading as a prescription drug benefit plan. The elderly will be offered some help with prescription drug costs. But only if they agree to enroll in private managed care insurance programs.</P>
<P>Most Americans won't be fooled by the "equal time" ploy in Bush's State of the Union address. Many will conclude--rightly--that Bush is spending as much time planning and plotting for an unjust war on Iraq as he is scheming and maneuvering for an unjust war at home.</P>


