<P><font face="Times New Roman, Times, serif" size="5"><b>An activist caught in the web of the INS</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 31, 2003 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><B><P>Dear <I>Socialist Worker,</P>
</B></I><P>On December 3, Roger Calero, an editor of the left-wing Spanish newspaper <I>Perspectiva Mundial,</I> affiliated with the U.S. Socialist Workers Party, was arrested by the Immigration and Naturalization Service (INS) as he returned from a trip to Cuba and Mexico. He now faces deportation.</P>
<P>Calero, originally from Nicaragua, has been a legal U.S. resident for the past 12 years--working as a journalist and an activist during that time. In November, he had traveled to Cuba for an international conference in Havana on the Free Trade Area of the Americas agreement--and then had gone to Mexico for the 16th Congress of Latin American Students.</P>
<P>But when he attempted to re-enter the U.S., he was arrested--supposedly because in 1989, when he was a high school student, Calero was convicted of selling an ounce of marijuana to an undercover cop. The conviction already has been waived twice by the INS--first when Calero applied for permanent residency in 1990, and then when his residency was renewed in 2000. </P>
<P>Authorities aren't going after Calero because of a 13-year-old minor drug conviction. They're attacking him because he's a socialist and an activist.</P>
<P>On December 13, Calero was released from a Houston prison, pending a March 25 INS hearing aimed at permanently removing him from the United States.</P>
<P>The only way to protect our civil liberties is to make sure that we raise our voices against attacks like these. An injury to one is an injury to all.</P>
<B><P>Nicole Colson,</B> Chicago</P>


