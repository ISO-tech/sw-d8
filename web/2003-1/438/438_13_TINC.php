<P><font face="Times New Roman, Times, serif" size="5"><b>Tying music to the struggle</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 31, 2003 | Page 13</font></p>

<font face="Times New Roman, Times, serif" size="3"><P>THE (INTERNATIONAL) Noise Conspiracy isn't your typical band. The Swedish group combines punk rock, soul and radical politics into music that hopes to move a new audience to activism. They made their mark playing at global justice protests in Genoa, Italy, and Gothenburg, Sweden, in 2001.</P>
<P>With band members' politics influenced by a wide range of ideas, including anarchism and socialism, T(I)NC's music defies easy classification. Their latest album, <I>A New Morning, Changing Weather,</I> takes up issues such as gay rights, imperialism and the possibilities for fighting for a different future. "It's just a record. It's not going to change the world," proclaims their Web site. "But still, it's very much a product of an ongoing struggle that, in the end, will change the world."</P>
<P>Lead singer Dennis Lyx&eacute;n spoke with SAM COFFMAN and NICOLE COLSON before a recent show in Chicago.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<P><b>WHAT DRAWS you to the protests for the global justice movement? And how do you think people can get involved?</b></P>

<P>I THINK what makes music powerful is when you can tie the music or the ideas to a social movement. A lot of my favorite protest music has always been music that was a part of a social change&#133;</P>
<P>What we always try to tell people and we try to do ourselves is to get involved in something that matters to you. Some might start a band like we did. Some might organize a protest group or some might organize a study circle. I think it's all about finding a way that a political struggle makes sense to you.</P>
<P>A lot of times, people force themselves to become a clich&eacute; image of revolutionaries&#133;like "Oh, I have to smash this many windows in order to be a revolutionary." Self-sacrifice is not really what we need. There's so much stuff that we could do together.</P>
<P>I mean, that's why we're a band. We're five people that got together because we realized that the sum of us five together is so much greater than just one of us. That's the kind of thing that we always tell people: Get educated and get organized. Overthrowing capitalism is not an easy task. It's not something you do by starting a band. But if you are one person in this big network of people working toward the same goal, then hopefully we'll make a little bit of difference.</P>

<P><b>WHAT ARE the limits of political music?</b></P>

<P>IT CAN change lives, but not the world. You can't really start a rock band and hope to get the economic structure of the world to crumble. But you can make people excited about it. </P>
<P>We always said that we picture ourselves as when the revolution comes, that we're that band that plays on the side of the road with people kind of dancing by. And while we're here, [we] inspire ourselves and get people involved in different ways.</P>

<P><b>YOUR LATEST album takes up many different issues. Why?</b></P>

<P>BECAUSE THERE'S so much stuff out there. Always, the basis of our analysis is an anti-capitalist, Marxist, socialist kind of understanding of the world. But we also wanted to show people how it affects our everyday lives. How do economic structures affect our sexuality? We need to show the connections&#133;</P>
<P>I'm not sure that everybody that listens to our record is going to get involved with defending the rights of transsexuals, but at least we show people that that's an issue that we need to talk about.</P>

<P><b>HOW DO you describe your politics generally?</b></P>

<P>IT IS a mix. We're all radical leftists. We have a class consciousness. We're radicals. We're revolutionaries. But we don't want to define our revolutionary ideas to an ideology or a dogma, and say "I am a Marxist-Leninist." And&#133;if you sit down and talk to us one by one, one guy is a bit more anarchist and one guy is a bit more communist than the other.</P>
<P>But what we wanted to do is present this smorgasbord of ideas to people and say, "What do you like about this? What makes sense to you?"</P>

