<P><font face="Arial, Helvetica, sans-serif" size="2"><b>WHAT WE THINK</b></font><BR>
<font face="Times New Roman, Times, serif" size="4">White House declares it's ready to go it alone in Iraq war</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Washington lays down the law</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 31, 2003 | Page 3</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>GEORGE W. BUSH'S mad drive to war ran into opposition last week from Washington's European "partners." French and German officials joined counterparts in China and Russia in hinting that they might oppose a U.S. effort to push a resolution through the United Nations (UN) Security Council for military action against Iraq.</P>
<P>"Don't expect Germany to approve a resolution legitimating war," German Chancellor Gerhard Schr&ouml;der--who won re-election last year by challenging Bush's war drive--told a rally of his Social Democratic Party. </P>
<P>So Bush sent his administration's supposed "dove," Secretary of State Colin Powell, to the World Economic Forum meetings in Davos, Switzerland, to send a hawkish message. "Multilateralism cannot become an excuse for inaction," Powell told the gathering of world business and political leaders. "We continue to reserve our sovereign right to take military action against Iraq alone or in a coalition of the willing," Powell added, invoking the September 11 attacks and the still unproven, though often repeated, claim that Iraq has links to the al-Qaeda network.</P>
<P>While it's refreshing to hear someone show some backbone and take on Bush, no one should expect much from this "den of thieves"--as the Russian revolutionary Lenin called the UN's predecessor, the League of Nations. Remember that there was speculation last November about the opposition Washington would face when it demanded a Security Council resolution on inspections. But for all the talk, that vote was unanimous.</P>
<P>If Schr&ouml;der and other European leaders are speaking up now about Bush's war drive, it's not because they oppose the horror that Bush has in store for Iraq. What France, Germany, Russia and China are really concerned about is what they might miss out on if the U.S. gets to call the shots in a post-Saddam Iraq. In a word, oil. France and Russia, in particular, have oil exploration contracts negotiated with Saddam Hussein that would be torn up if the U.S. takes control of Iraq's oilfields.</P>
<P>And it's not just about oil, but Washington's spreading military and political control. The U.S. military has bases in 60 countries and troops in some 130, and a war on Iraq will extend its tentacles even further.</P>
<P>Even if the Bush administration continues to get flak from Europe, don't think that this by itself will derail the war drive. "I remember a similar position in 1991 when Bush's father was assailed by allied doubts," an unnamed U.S. diplomat told Scotland's <I>Sunday Herald</I> newspaper. "It was particularly strong in France, but once the fighting began, they and other doubting Thomases put their fears behind them and supported the war. Besides, you can bet that France and Germany will be singing a different tune once Saddam has been unseated, and there are opportunities to benefit from postwar reconstruction."</P>
<P>There is a real antiwar opposition standing up to Bush in countries around the world--but it won't be found in the parliaments and presidential palaces. Polls show wide majorities of people opposed to a war on Iraq across Europe, and huge demonstrations have given voice to this sentiment, from Italy to Britain.</P>
<P>Even in the U.S., Bush is facing an antiwar movement that has grown with dramatic speed, mobilizing half a million people to march in Washington, D.C., San Francisco and other cities across the country on January 18. At the European Social Forum last year, antiwar groups called for a joint day of protest on February 15 that is expected to bring out even larger numbers. In the U.S., activists are planning for a national mobilization in New York City, plus smaller protests across the country.</P>
<P>This is the kind of "international community" that can pose a real challenge to Bush's war for oil--opposition at the grassroots and united in international solidarity against a new U.S. slaughter in Iraq.</P>


