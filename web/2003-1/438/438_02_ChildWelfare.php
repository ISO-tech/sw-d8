<P><font face="Times New Roman, Times, serif" size="4">Child abuse tragedy in N.J.</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Blame the cuts, not caseworkers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Wengraf</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 31, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>THE STARVATION and beating death of a 7-year-old boy in New Jersey just after the New Year has once again exposed the terrible cost of cutbacks in state programs for the poor.</P>
<P>Two starved children, Raheem Williams and Tyrone Hill, were found locked in the basement of a Newark house, where they were hiding, malnourished and dehydrated, under a bed. The next day, the body of their brother Faheem Williams was found in a plastic storage container in the same basement.</P>
<P>The boys' aunt and her sixteen-year old son were both arrested, but the media's focus has been on New Jersey's child welfare agency. There had been allegations of abuse against the three boys, but caseworkers for the Division of Youth and Family Services (DYFS) had not followed up with a visit in almost a year. According to media reports, the whereabouts of 280 children with complaints of abuse or neglect are unknown. Today, 21 remain "missing."</P>
<P>New Jersey Gov. James McGreevey has declared a "state of emergency." But in a repeat of last year's tragedy in Florida--where the state child welfare agency lost track of 5-year-old Rilya Wilson for 15 months before realizing she was missing--McGreevey's main target is the caseworkers, not the underfunded welfare system.</P>
<P>New Jersey's DYFS has 1,400 people to supervise some 47,000 children. That puts the average caseload at 35, well above the 25 recommended by national advocates. And union officials say that the caseworker who was assigned to the Williamses was juggling 107 cases.</P>
<P>When McGreevey held a press conference to announce that three workers had been suspended, union members threatened to walk off the job. "Because of the work of the union, we moved the debate from blaming individual workers to the huge systemic problems they want to leave at the door of caseworkers," said Hetty Rosenstein, president of Communications Workers of America Local 1037, which represents the caseworkers.</P>
<P> "The union has waged a six-year struggle to expand resources and the budget. We've had to repair the damage done over many, many years--the major budget cuts from before and staff vacancies allowed to go unfilled for a long time. But the system is heavily stressed because of poverty."</P>
<P>Further budget cuts being considered by the state government will push this crisis over the edge. "Our workers do believe there's a state of emergency at DYFS," Rosenstein told <I>Socialist Worker.</I> "We've been saying that for years. We're the ones that fight for more services--not the administrators, and not the advocates."</P>


