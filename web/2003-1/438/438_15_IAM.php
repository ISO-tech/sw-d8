<P><font face="Times New Roman, Times, serif" size="4">Why won't IAM leaders challenge wage cuts?</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>United goes after unions</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Jennifer Biddle, IAM Local 1781</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 31, 2003 | Page 15</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>UNITED AIRLINES successfully petitioned a bankruptcy judge to unilaterally cut wages 14 percent for workers represented by the International Association of Machinists (IAM) District 141M. United also warned all employees that if workers didn't voluntarily accept additional concessions in the coming months, the company would ask the court to terminate its union contracts in mid-March.</P>
<P>IAM District 141M represents approximately 9,000 aircraft mechanics and 1,500 utility workers. These employees are the only group that consistently voted concessions down, both during contract negotiations early last year and when the company demanded cutbacks before filing for bankruptcy in December.</P>
<P>In a twist that no one expected, the IAM refused to negotiate during this latest round with the company and claimed to take a stand against concessions. All this did was give management exactly what it wanted--a reason to petition Judge Eugene Wedoff for unilateral cuts without taking the chance of another vote by the mechanics and utility workers.</P>
<P>If the union had been serious about taking a stand against concessions, it would have demanded a vote and mobilized people to cast ballots against cuts. Instead, the IAM continues to play a treacherous role--at best denying information to the membership, and at worst making it easier for the company to get what it wants. </P>
<P>In the "Q&amp;A" on the union Web site, the IAM gives employees its version of a choice should the judge agree to abrogate our contracts--accept the terms, or quit and find another job. Most mechanics, however, want to resist the changes that United and the rest of the airline industry are pushing through. </P>
<P>"It would be the proudest day of my life if every mechanic at every airline in this country packed up all their tools and walked off their jobs at the same time," said one mechanic, summing up the general mood.</P>
<P>Two months before United filed for bankruptcy, the Air Transport Association (ATA) met to discuss restructuring the industry. The ATA is the trade organization that represents all the major carriers--United, American, Delta, Southwest, JetBlue, Continental and 17 others. </P>
<P>In the summary report of the conference, the ATA concludes that layoffs, concessions from labor, outsourcing jobs and the "Railway Labor Act" (RLA) debate are "bright spots" for the industry. The "RLA debate" is a reference to legislation that the ATA is lobbying heavily for that would amend the RLA to prevent air transport workers from striking at all.</P>
<P>In truth, the older, bigger airlines can't compete with airlines like JetBlue and Southwest. Southwest, for example, outsources about 70 percent of its maintenance and flies only one fleet-type to secondary airports. The bankruptcy courts are acting as a means for the industry to accomplish this goal. </P>
<P>Union leaders' lack of fight will force rank-and-file workers themselves to take up the task of defending their jobs. Mechanics at United plan to petition the bankruptcy court on their own behalf to demand an investigation of United's finances and to ask the judge to establish a "workers' committee" so we have a say in the court's proceedings.</P>


