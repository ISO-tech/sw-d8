<P><font face="Times New Roman, Times, serif" size="5"><b>New York City transit workers</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Hadas Thier, Michael Ware and Lee Wengraf</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 31, 2003 | Page 15</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--More than a month after union officials struck a deal to avoid a transit strike, members of the Transport Workers Union (TWU) Local 100 have approved their new contract with the Metropolitan Transportation Authority (MTA).</P>
<P>Turnout for the ratification vote was at a record high, with roughly 20,000 of the TWU's 34,000 members casting ballots. The deal passed by a margin of only 60 percent, reflecting rank-and-file frustration with the contract.</P>
<P>The new deal gives workers a $1,000 lump sum bonus in the first year and raises of 3 percent in both of the next two years of the three-year contract. Because of the low pay increases and a weakened "no-layoff" clause, some workers called for a "no" vote. Recording Secretary Noel Acevedo ran a high-profile statement in the <I>New York Times</I> calling on members to reject the contract.</P>
<P>The nearly 8,000 members who rightly rejected the concessionary offer represent a blow to Local 100 President Roger Toussaint. He will likely try to deflect anger at the deal onto Sonny Hall, the TWU's International president, by saying that the International was organizing against the contract to discredit Local 100 leaders.</P>
<P>In 2001, Toussaint, a reformer, challenged Hall for the TWU presidency and lost, resulting in a bitter feud. When Toussaint agreed to this lousy deal, Hall sided with his longtime adversaries--grouped around Rank-and-File Advocate in Local 100--to call for a "no" vote.</P>
<P>With Mayor Michael Bloomberg demanding cuts and threatening layoffs throughout city agencies, this agreement will be used by the city to demand concessions from other city unions. </P>
<P>A strong rank-and-file current has to be rebuilt within Local 100. Otherwise, reformers like Toussaint--who dismantled the reform movement that elected him--will back down in the face of future legal and financial threats. </P>
<P>With the stakes this high, only a credible threat of revolt from below could have won a good contract.</P>

<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<P><font face="Times New Roman, Times, serif" size="4"><b>"An educated union is a strong union"</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>RICHARD WATSON, a longstanding steward in the station division of TWU Local 100, spoke with <I>Socialist Worker</I> about organizing a "no" vote.</P>

<B><P>WHAT DO you think of the new contract?</P>
</B><P>WHEN YOU want to talk about the major issues, for instance health benefits, we're still faced with increased co-pays. The wages&#133;I'm still waiting for someone to tell me how this is going to help me keep up with inflation, and New York City taxes being raised. </P>
<P>We used to have a very clear no-layoff clause that included no furloughs and no transfers, but the layoff clause we have in this contract is not as strong. </P>
<B><P>IN THE run-up to negotiations, TWU leaders offered a plan for the MTA to better manage its funds. What did you think of that strategy?</P>
</B><P>THE UNION is supposed to represent labor, not management. We shouldn't get into the business of management's work. There's a term being used now--"from confrontation to cooperation." It means nothing--I don't see any cooperation [from the MTA].</P>
<P>Confrontation means that we can put real pressure on management to the point where they're backing down. Cooperation is the union saying that we want you to give us a little something, but you've already submitted yourself to what they wanted in the first place. And when you look at this contract, it's more cooperation than confrontation.</P>
<B><P>WHY DO you think people voted "yes"?</P>
</B><P>FEAR. THERE were people who were afraid of going on strike. People were afraid we'd get worse. And this idea was promoted by some people in our union leadership. What they should have done was to educate the members.</P>
<B><P>WHAT DID you do in response to the settlement?</P>
</B><P>I TRIED to organize an educated vote. I'm going to tell people the truth. I'm not going to let you bring this contract and have it presented as though it's this bright thing gleaming in the sky. It had clouds around it. I wanted to clear the clouds. </P>
<P>I got some literature out on the road. I also did as much e-mailing as possible and holding conversations with other workers. It was successful. One night, I got on my computer, and my e-mail was up to 177 replies.</P>
<P>An educated union is a very strong union. We need to be more serious about getting people involved.</P>


