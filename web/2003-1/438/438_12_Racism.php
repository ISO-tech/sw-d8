<P><font face="Times New Roman, Times, serif" size="4">Politicians of every stripe tolerate bigotry</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Racism on the agenda</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 31, 2003 | Page 12</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>STANLEY HOWARD is one of four Illinois death row prisoners pardoned by outgoing Gov. George Ryan--though he remains in prison on other trumped-up charges.</P>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>
<font face="Times New Roman, Times, serif" size="3">
<B><P>Dear <I>Socialist Worker,</P>
</B></I><P>The nation would have been "better off" if Strom Thurmond had won the presidency when he ran on a segregationist ticket in 1948, boasted a cheerful Sen. Trent Lott (R-Miss.) while attending Sen. Strom Thurmond's (R-S.C.) 100th birthday bash.</P>
<P>Coming while the Supreme Court was hearing a case that could curtail affirmative action and another case to determine if cross-burning is constitutionally protected, Lott's comments pushed the question of racism in America back on the agenda, and had many calling for his resignation.</P>
<P>The reason why Lott's comments had many in an uproar is not only due to racism being wrong and immoral, but also because there's an unspoken and unwritten rule in America--that you can be a racist, as long as you don't openly express your racist views and opinions publicly.</P>
<P>At the same time, you can use your racist views and opinions to rain hell and misery in people's lives; to keep the races separated, divided and politically, economically and socially unequal; and to instill fear, terror and distrust domestically and abroad. This explains why Lott is being rebuked for glorifying Thurmond's racist position--but Thurmond himself is not being and has never been rebuked.</P>
<P>Racism is so systematically embedded in the fabric of America's institutions, culture and society that having known racists in leadership positions really shouldn't surprise anyone.</P>
<P>This same racial hatred is a major component that fuels the U.S. judicial system and its death penalty. And having a death penalty in the midst of this racially charged environment amounts to nothing more than a modern-day form of lynching that they claim to be constitutional--like cross-burning. Blacks are 12 percent of the U.S. population but 40 percent of those on death row nationally and two-thirds of those on Illinois' death row. </P>
<P>Yes, we overcame the appearance of slavery, but the hatred and ignorance that enabled it to thrive hasn't been eradicated. We overcame some forms of segregation and obtained some civil rights, and we're progressively eliminating the hatred and ignorance--but still have a long way to go.</P>
<P>The best way for America to start showing that all life is equally valued and racism won't be tolerated is to stop supporting the racist death penalty and those who support it.</P>
<B><P>Stanley Howard,</B> Menard, Ill.</P>


