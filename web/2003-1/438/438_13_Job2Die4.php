<P><font face="Times New Roman, Times, serif" size="5"><b>Why work is bad for your health</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Carole Ramsden</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 31, 2003 | Page 13</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>BOOKS:</B> Lisa Cullen, <B><I>A Job to Die For: Why So Many Americans Are Killed, Injured or Made Ill at Work and What to Do About It.</B></I> Common Courage Press, 2002, 224 pages, $17.95.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>SUE, A nurse at a long-term nursing facility, had told her boss for weeks she needed a hoist to lift one of her patients. Her boss repeatedly denied her requests. One day, the patient's legs collapsed when Sue was lifting him. The fall broke Sue's neck. Seven years later, after multiple surgeries, pain and fighting with workers compensation, Sue has resigned herself to life of constant pain and steadily deteriorating health.</P>
<P>This is only one of the many gut-wrenching stories in Lisa Cullen's book, <I>A Job to Die For.</I> "Legalities, politics and money have overridden the human rights issue of worker health and safety. Worker health and safety is becoming nothing more than a business line item," she writes.</P>
<P>Every day, 165 Americans die from occupational disease, and 18 more die from work-related injuries. The costs of occupational injury, disease and death is nearly $156 billion annually, five times the cost of AIDS, three times the cost of Alzheimer's and nearly as much as cancer.</P>
<P>Throughout this book are the personal stories of workers struggling to fight for justice and dignity after an injury or illness to--or worse yet, the death of--a loved one. Many of these stories illustrate how the workers compensation program has ended up as "nothing more than a manipulative business expense."</P>
<P>In the case of the Occupational Safety and Health Administration (OSHA), Cullen shows that its "efforts to protect worker welfare are no match against the wealth of the industries it is supposed to regulate."</P>
<P>But it's not just big business and the insurance industry that Cullen targets. She also gets her licks in against Congress, which has turned OSHA into a whipping boy in crusades against federal regulation. "Congress shares much, if not all, of the responsibility for OSHA's state. Time and time again, Congress takes steps to thwart OSHA rather than help it," Cullen writes.</P>
<P>The media campaign against workers hasn't helped either, creating a stigma many workers face, both at the job and at home. Even though studies have shown that only 1 to 2 percent of workers' compensation claims are fraudulent, public perception views the injured or ill workers as lazy or "scam artists." For the workers, "it has become acceptable to assume guilt until proven innocent, burdening them with an indignity that is cruel, chronic and unjust."</P>
<P>While much of the book details how workers' compensation and OSHA have been manipulated, outspent and gutted, only a small part is dedicated to a solution. While <I>A Job to Die For</I> diligently defends workers from page one, unfortunately it includes almost nothing about what workers' own organizations--unions--can do. Reliable reporting, proper media coverage and public oversight are the biggest part of Cullen's cure.</P>
<P>The facts are in--workers unnecessarily face injury and death on the job. What's needed now is for workers to organize a resistance to these horrors in our workplaces that the employers--and the government--can't ignore.</P>

