<P><font face="Times New Roman, Times, serif" size="4"><b>George W. Bush gets boxed in</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>WHEN GEORGE W. Bush gave a speech touting tax breaks for small businesses, he stood against a backdrop of cardboard boxes stamped "MADE IN U.S.A." Or so it appeared.</P>
<P>Turns out, the boxes in the south St. Louis warehouse had been painted on a large screen behind the president. And the real boxes in the warehouse were stamped "Made in China"--although someone tried to obscure the stamps by plastering over them with blank white labels.</P>
<P>Asked about one of the labels by the <I>Washington Times,</I> Randy Shore, the warehouse manager for J.S. Logistics, smiled and said: "I don't know how it got there." He later claimed that the labels would be filled in with numbers to identify their location on racks throughout the warehouse. Pressed on why the stickers were placed precisely over the "Made in China" stamps, he added: "That's as good a place as any."</P>
<P>The White House later acknowledged the stickers were affixed intentionally by "an overzealous volunteer."</P>
<P>--Associated Press, January 22, 2003</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Emma causes a ruckus</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ANARCHIST EMMA Goldman is still causing a stir--some 62 years after her death. This time, it's at the University of California-Berkeley, which houses Goldman's papers.</P>
<P>University officials have refused to allow a fundraising appeal for the Emma Goldman Papers Project to be mailed because it quoted Goldman on the subjects of suppression of free speech and her opposition to war. </P>
<P>In one of the quotations, Goldman called on people "not yet overcome by war madness to raise their voice of protest, to call the attention of the people to the crime and outrage which are about to be perpetrated on them." </P>
<P>In the other, she warned that free-speech advocates "shall soon be obliged to meet in cellars, or in darkened rooms with closed doors, and speak in whispers lest our next-door neighbors should hear that free-born citizens dare not speak in the open."</P>
<P>Berkeley officials said the quotations could be construed as a "political statement" in opposition to U.S. policy toward Iraq. </P>
<P>--<I>New York Times,</I> January 14, 2003</P>

<P><font face="Times New Roman, Times, serif" size="4"><b>Hollywood goes to war</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>RAMBO IS back. And this time he's gunning for Osama bin Laden.</P>
<P>Hollywood veteran Sylvester Stallone is set to return to the screen in <I>Rambo IV</I>--after writing the script himself. The movie is expected to hit theaters next year, and will feature action in Afghanistan and battles with the Taliban.</P>
<P>A movie insider said: "The original story had Rambo killing bin Laden single-handed, but even Sly thought that was beyond the imagination. Instead, he will be the brains behind bin Laden's downfall."</P>
<P>--<I>The Sun (UK),</I> January 20, 2003</P>

