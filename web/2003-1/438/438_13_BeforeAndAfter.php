<P><font face="Times New Roman, Times, serif" size="5"><b>Bush's "what we say goes" foreign policy</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>Review by Ashley Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 31, 2003 | Page 13</font></P>
<font face="Arial, Helvetica, sans-serif" size="2"><B><P>BOOKS:</B> Phyllis Bennis, <B><I>Before and After: U.S. Foreign Policy and the September 11 Crisis.</B></I> Olive Branch Press, 2002, 264 pages, $17.95.</P></font>
<font face="Times New Roman, Times, serif" size="3"><P>SEPTEMBER 11, the war on Afghanistan and the looming invasion of Iraq have ushered in a new epoch of international relations. The Bush administration's foreign policy doctrine aims to lock in U.S. economic dominance through military intimidation not only against so-called "rogue states" but against any and every competitor. Phyllis Bennis' new book <I>Before and After</I> traces this shift in foreign policy before and after September 11.</P>
<P>Well before Bush entered the White House, Bennis points out, U.S. foreign policy was making its way toward a unilateralist, "what we say goes" approach. The Clinton administration maintained its control through what Bennis calls "assertive multilateralism."</P>
<P>"That commitment," she writes "was always more rhetorical than real, as Clinton was far more committed to the appearance of leading a global coalition than to any real consultation, let alone power sharing, in global decision-making."</P>
<P>Bush tore off the multilateral cover with an aggressive unilateral approach--ditching the Anti-Ballistic Missile Treaty, for example. But it was September 11 that gave Bush the opportunity to bring any international opposition to heel and launch military operations everywhere it chose.</P>
<P>The Bush administration disguised this new imperialism as a war on terror against fanatical "evil doers" who attacked an innocent and benevolent U.S. Bennis rips apart this illusion of the U.S. She chronicles U.S. history in the Middle East, denying countries the right to self-determination, installing friendly dictatorships and bankrolling the Israeli occupation of Palestine.</P>
<P>Likewise, <I>Before and After </I>takes up the real aims of the war on Afghanistan. "The war was never about bringing anyone to justice," she writes, "it was about conquest and the mushrooming of U.S. global power, all in the name of righteous vengeance." Bennis concludes by demolishing Bush's case for war on Iraq.</P>
<P>The strength of Bennis' book is the abundance of information she provides. However, she balks at some logical conclusions. For example, Bennis calls for the U.S. to support a United Nations (UN)-sponsored police action to bring the perpetrators of September 11 to justice.</P>
<P>There are several problems with this argument. First, it bolsters the idea that this war is about terrorism, which Bennis herself disproves. Second, such calls for justice will either be ignored or happily used by the U.S. as a cover for its imperial motives. Finally, this was the basis on which many liberals and even leftists supported the disastrous war on Afghanistan.</P>
<P>Similarly, her belief that the UN can be a counterweight to U.S. unilateralism is a dangerous illusion. As Bennis knows, historically the U.S. has either ignored the UN or used it to further its own interests.</P>
<P>Real solutions won't come from the U.S. or the UN, but from a movement of working people uniting across borders against war and inequality.</P>


