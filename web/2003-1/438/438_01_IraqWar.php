<font face="Times New Roman, Times, serif" size="5"><b>He'll turn Baghdad into hell on earth</b></font></P>
<p><font face="Arial, Helvetica, sans-serif" size="2">January 31, 2003 | Page 1</font></p>
<font face="Times New Roman, Times, serif" size="3"><P>"THERE WILL not be a safe place in Baghdad." Those are the chilling words of an unnamed Pentagon official describing the planned U.S. war on Iraq to <I>CBS News</I>. For sheer brutality and violence, the strategy--called "Shock and Awe," according to CBS--is nearly unmatched in the history of warfare.</P>
<P>During the first 24 hours of the assault, U.S. forces will fire 300 to 400 cruise missiles. That's more than were used during the entire 40 days of the first Gulf War in 1991. And on the second day of the war--another 300 to 400 cruise missiles.</P>
<P>What will this mean in a city like Baghdad--home to more than 5 million people? Harlan Ullman, a Pentagon adviser and one of the authors of the "Shock and Awe" strategy, told CBS that the plan is to "take the city down." "By that, I mean you get rid of their power, water," Ullman said. "In two, three, four, five days, they are physically, emotionally and psychologically exhausted."</P>
<P>In other words, terrorized into submission. "We want them to quit," Ullman said. "We want them not to fight. So that you have this simultaneous effect, rather like the nuclear weapons at Hiroshima, not taking days or weeks, but in minutes." </P>
<P>In fact, the war on Iraq could look exactly "like the nuclear weapons at Hiroshima." According to <I>Los Angeles Times</I> columnist William Arkin, the Pentagon is "thinking about the unthinkable: It is preparing for the possible use of nuclear weapons against Iraq&#133;The Bush administration's decision to actively plan for possible preemptive use of such weapons, especially as so-called bunker busters, against Iraq represents a significant lowering of the nuclear threshold. It rewrites the ground rules of nuclear combat in the name of fighting terrorism."</P>
<P>The Bush gang is making it clear who the real terrorists are. They work at the White House and the Pentagon.</P>
<P>The leaks of the war plans came before chief weapons inspector Hans Blix made his report to the United Nations. Blix said that there was no "smoking gun"--hard evidence of an arsenal of weapons of mass destruction.</P>
<P>But that doesn't matter to the U.S. government. As <I>Socialist Worker</I> went to press, George W. Bush was preparing to give a "State of the Union" address calculated to whip up a war frenzy--including, according to press reports, the unfounded charge that Iraq was somehow connected to the September 11 attacks.</P>
<P>The Bush gang's message is unmistakable: We'll get our war, no matter what. We can't let them get away with this slaughter. We have to stand up and say no to Washington's war on Iraq!</P>


