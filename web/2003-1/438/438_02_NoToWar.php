<P><font face="Times New Roman, Times, serif" size="4">Say no to Bush's war on Iraq</font><BR>
<font face="Times New Roman, Times, serif" size="5"><b>Celebrating King Day by opposing the war</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 31, 2003 | Page 2</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>ACTIVISTS USED Martin Luther King Day on January 20 as an opportunity to celebrate the slain civil rights leader's antiwar legacy by protesting the pending U.S. war on Iraq.</P>
<P>In Seattle, 10,000 people took to the streets with the demand, "Support the poor, stop the war!" Every year, Seattle's King Day commemoration highlights a political theme. This year, after much debate, organizers decided to honor the last year of King's life, when he began crusading against the Vietnam War, calling the U.S. government "the greatest purveyor of violence" in the world.</P>
<P>In Atlanta, the 10,000-strong Martin Luther King Day march was transformed into an antiwar protest. King's speech condemning Vietnam was played on loudspeakers.</P>
<P>In Washington, D.C., after a weekend of some of the largest antiwar demonstrations in decades, activists turned out for a teach-in sponsored by Black Voices for Peace. The standing-room-only crowd packed into the Plymouth Congregational United Church of Christ to hear former Rep. Cynthia McKinney (D-Ga.), Ralph Nader and former Student Nonviolent Coordinating Committee member James Forman speak against war at home and abroad.</P>
<P><font face="Times New Roman, Times, serif" size="4"><b>Getting organized</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>On January 7, members of Rochester Institute of Technology (RIT) Antiwar marched into the school's Center for Integrated Manufacturing Studies (CIMS) to conduct their own weapons inspection.</P>
<P>The center receives millions of dollars from the Pentagon to conduct research and maintenance of weapons for the military. Students dressed up as an inspection team and searched the CIMS building--highlighting the absurdity of the U.S. demand that Iraq open up to inspectors when the U.S. has the largest arsenal of weapons of mass destruction.</P>
<P>In Washington, D.C., activists are planning a protest concert and rally in front of the U.S. Capitol on January 28 during Bush's State of the Union address. "The Sorry State of the Union" will feature performances by Thievery Corporation, Mr. Lif and other artists, along with speakers from labor, human rights and political organizations. For details, go to www.thecriticalmoment.org/union.</P>
<P>At the University of Texas at Austin on January 24, some 400 students, faculty and staff protested Bush's war plans. In California, the Berkeley Stop the War Coalition held its first event of the semester January 23, a teach-in that drew more than 100 people.</P>
<P><font face="Times New Roman, Times, serif" size="4"><b>All out February 15!</b></font></P>
<font face="Times New Roman, Times, serif" size="3"><P>On the East Coast, antiwar groups are preparing for a national protest in New York City called by United for Peace and Justice for February 15. Some 175 people attended an organizing meeting for the event on January 23.</P>
<P>The Columbia University Antiwar Coalition began the semester with 40 people at its first meeting to help organize a student rally in conjunction with the February 15 event. Energized by the massive January 18 protest, members of the New York University Peace Coalition are organizing a week of action on campus leading up to the protest. On January 27, several hundred protested the war at the United Nations, where Hans Blix presented his weapons inspections report.</P>
<P>In addition to building the national protest, the Hunter Antiwar Coalition is organizing to demand the release of Kharram Ali, a Pakistani student at Hunter College who was detained by the Immigration and Naturalization Service (INS) for an outstanding tuition bill.</P>
<P>No blood for oil! Free the detainees!</P>
