<P><font face="Times New Roman, Times, serif" size="5"><b>Fight for abortion rights</b></font></P>
<P><font face="Times New Roman, Times, serif" size="2"><b>By Lee Wengraf</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | January 31, 2003 | Page 14</font></P>
<font face="Times New Roman, Times, serif" size="3"><P>NEW YORK--On the 30th anniversary of the <I>Roe v. Wade</I> decision that legalized abortion, 100 activists gathered in Union Square to demand that abortion be kept safe and legal. </P>
<P>Carrying signs reading "Keep your laws off my body" and "Protect our right to choose," speakers from the Green Party, the New York City chapter of the National Organization for Women (NOW) and New York University's Voices for Choice denounced George W. Bush's and other politicians' attacks on abortion access. "It's no coincidence that as Bush is planning a war on the world, he's launching a war on women here at home," said Elizabeth Shanklin, a Green Party candidate for Congress in 2002.</P>
<P>Unfortunately, both the small turnout at New York's rally and a message that put sole blame on the Republicans--when Democrats have equally voted for abortion curbs--speaks to the major steps we need to take to re-build a fighting movement. Likewise, the rally's emphasis on "common ground" with the anti-choice movement represents a huge retreat from the demands of the 1970s--free abortion on demand without apologies.</P>
<P>This is one of the reasons why pro-choice demonstrations in other cities remained small--as mainstream groups like NOW were reluctant to call for the kind of large mobilizations that could stand up to the anti-choice bigots.</P>
<P>In Chicago, only about 50 pro-choice demonstrators turned out, and in Washington, D.C., a pro-choice rally was overwhelmingly outnumbered--as up to 50,000 anti-choice protesters celebrated Bush's attacks on abortion rights.</P>
<P>We have to remember how abortion rights were won in the first place--in the streets, not by relying on the Democrats or the courts. "All organizations need to come together, men and women alike, to say that pro-choice is the majority, not the minority," Robin Sklar, co-chair of the Green Party Manhattan Office, told <I>Socialist Worker</I>. "Everyone has the fundamental right to make their own decision on abortion."</P>

