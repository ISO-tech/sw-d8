<font face="Times New Roman, Times, serif" size="5"><b>Muslim's murder shakes Fremont</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By John Gallagher and Todd Chretien</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 3, 2006 | Pages 1 and 2</font><p>

<font face="Times New Roman, Times, serif" size="3">THE CITY of Fremont, Calif., was shocked by the October 19 murder of Alia Ansari, a Muslim woman, while she was walking to pick up her children from Glenmoor Elementary School.<p>
A driver of a black Toyota or BMW stopped, got out of the car and shot her in the head, killing her immediately. She continued to hold onto her 3-year-old daughter's hand so that the toddler wouldn't go into the busy intersection nearby.<p>
Ansari's family and friends fear Alia was targeted because she was wearing a hijab (head scarf) and that her murder was a hate crime, the latest and most horrific consequence of the rising wave of anti-Arab and anti-Muslim racism.<p>
The 38-year-old Ansari was a mother of six and well known in the community as caring, loving and an extraordinary cook. She and her husband, Ahmed, emigrated to the U.S. in 1986 and settled in Fremont (known as Little Kabul for its large Afghan population).<p>
Hundreds of people attended the October 27 memorial service for Ansari in Fremont's Central Park. Sheikh Hamza Jusuf, who led the service, reminded the assembled community members that Alia's last act was to selflessly protect her daughter from violence.<p>
As he passed a box around the crowd to gather donations for the Ansari family, David Haidary said that Alia was his best friend's cousin. "So my heart's really into doing something," he said. "I hope we can find out what happened. We're gathering contributions to try to move her family into a better neighborhood. The community has been more unified in the last few years, but that means it's also become more of a target."<p>
The day after the Central Park memorial service, Fremont residents gathered to pay their respects and raise money for the family at the Centerville Presbyterian Church. Catholics, Jews and Muslims all attended the interfaith memorial. An Imam thanked the crowd of about 100 mourners for the support the community has shown in this tragedy.<p>
"I feel like the faith communities came together in this tragic event to support the Muslim community," said Fremont Catholic Community member Mary Murray. "I felt honored that the Ansari family came to the memorial at the Christian church to help us mourn the loss of Alia, even though she was leaving for Afghanistan for her final resting place."<p>
Fremont police say they haven't found any leads in the Ansari case, other than a "person of interest" who happens to drive the same car that witnesses identified.<p>
The murder takes place in the context of growing Islamophobia stoked since 9/11 by the Bush administration and other politicians. Muslim men, women and children have been the targets of hate crimes, racial insults and harassment as a result.<p>
Last month, the Council on American-Islamic Relations (CAIR) reported that hate crimes against Muslims rose 30 percent in 2005 to 1,972 incidents. One in five of these crimes took place in California, according to CAIR.<p>
The official sanction for anti-Muslim racism has given further confidence to groups like the racist Minutemen Project, which was organizing in Fremont over the summer.<p>
The Fremont police have downplayed this explanation for the crime, calling it "irresponsible" to consider Ansari's killing a hate crime. This is the same police force that worked with the Minutemen Project over the summer--and, when a fight broke out during one demonstration, arrested several immigrant rights supporters, but not one Minutemen supporter.<p>
More events are being organized in the Fremont area. "This has shaken the entire community, " said Anu Natarajan, a member of the Fremont city council. "We will not tolerate something like this in Fremont."<p>
