<font face="Times New Roman, Times, serif" size="5"><b>South Dakota vote on abortion ban</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 3, 2006 | Page 5</font><p>

<font face="Times New Roman, Times, serif" size="3">THE OUTCOME of a ballot measure that would block a legislative ban on abortions in South Dakota was too close to call in the week before November 7 voting.<p>
In March, South Dakota Gov. Mike Rounds signed a bill that would make it a felony--punishable by five years in prison--for doctors to perform an abortion, even in cases of incest, rape or a threat to the health of the woman.<p>
Abortion rights supporters responded by taking the ban to voters. In just nine-and-a-half weeks, they gathered more than 38,000 signatures to get the No on Referred Law 6 referendum on the November ballot. According to a recent Zogby poll, opponents of the ban were narrowly leading, at 47 percent to 44 percent.<p>
In their zealousness to make sure the ban becomes law, abortion rights opponents circulated ads claiming the law includes exceptions for rape and incest. The truth is the only exception is if having an abortion would "prevent the death of a pregnant mother."<p>
Other anti-choice ads claim that women suffer psychological damage as a result of having an abortion. A group of women calling themselves the "Fleet for Little Feet" toured this summer to tell stories of how they suffered as a result of having an abortion.<p>
An important crusader behind the South Dakota ban is the "Vote Yes for Life" campaign's Leslee Unruh, whose group's signs announce, "I regret my abortion." "The face of this campaign [in South Dakota] has not been dead babies or babies, it's been the women," Unruh told the <i>American Prospect.<p>
</i>This flies in the face of most women's actual experience--and the conclusions of the American Psychological Association, which says that abortion has no lasting or significant health risks.<p>
At the heart of this anti-abortion argument is the idea that the law must save women from themselves--that the so-called "post-abortion syndrome" is the result of women going against their supposed nurturing nature.<p>
But if there was any illusion about the right's alleged concern for women, note the words of abortion ban supporter state Sen. Bill Napoli, when he asked when an abortion might be allowed.<p>
"A real-life description to me would be a rape victim, brutally raped, savaged," Napoli said on the <i>NewsHour </i>in March. "The girl was a virgin. She was religious." Napoli continued, "She planned on saving her virginity until she was married. She was brutalized and raped, sodomized as bad as you can possibly make it, and is impregnated. I mean, that girl could be so messed up, physically and psychologically, that carrying that child could very well threaten her life."<p>
People like Napoli and other state politicians--including five Democrats who made the difference in getting the anti-abortion law passed in the state Senate--see South Dakota as ground zero for a potential ban on abortion nationally.<p>
If South Dakota's ban survives the upcoming referendum, supporters hope to take the issue to the U.S. Supreme Court to challenge <i>Roe v. Wade, </i>the 1973 Supreme Court decision that legalized abortion. And if the Supreme Court left the ban intact, individual states would decide the future of abortion rights--and 13 state legislatures are reportedly ready to pass similar laws.<p>
The South Dakota case strays from the strategy that anti-abortion forces have used so successfully since abortion became legal--making the right to choose less and less accessible by imposing more and more restrictions<p>
Opponents of the ban have centered their argument on the idea that the law goes "too far" by not allowing exceptions for women's health, rape and incest. But this fails to take up the question of whether there should be any ban on abortion.<p>
South Dakota, like several other states, is an example of how chipping away at abortion has effectively taken away access to abortion for most women. South Dakota requires a 24-hour waiting period, mandatory counseling and parental notification for minors seeking abortion. There is just one clinic in the state that offers the procedure, provided by a physician who flies in from out of state just one day a week.<p>
While South Dakota is one of the worst, most states restrict women's access to abortion. On November 7, voters in California and Oregon will decide on ballot measures that would force minors to get parental consent before obtaining an abortion.<p>
It's possible that pro-choice forces are going to be able to push back the bigots November 7 in South Dakota. This would be a welcome defeat for the Religious Right, which thinks it can trample over a woman's right to decide what happens to her body.<p>
But it should be just the beginning--because we need to oppose each and every restriction on a woman's right to abortion.<p>

