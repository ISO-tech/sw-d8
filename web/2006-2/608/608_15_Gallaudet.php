<font face="Times New Roman, Times, serif" size="4">Protests force board to abandon Fernandes selection</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Gallaudet students win</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Chris Yarrison</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 3, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">WASHINGTON--After more than three weeks of protests, the students of Gallaudet University, the nation's only liberal arts college for the deaf, have forced the administration to concede to their demands.<p>
Since May, the protesters have demanded that newly selected president Jane K. Fernandes, who was to take up her position January 1, step down so that the Board of Trustees could reopen the search process. Students say that process overlooked more-qualified, but minority, candidates, and that Fernandes was not an effective provost during her past six years in that position at Gallaudet.<p>
Indeed, in the face of escalating, sustained protests, and opposition not just from students but from 82 percent of the faculty, Fernandes proved she was not fit to lead by stubbornly refusing to step down.<p>
However, with protesters swelling from 150 on October 6 to more than 2,000 last week, the Board of Trustees was finally forced to capitulate or face increasing disruption to university business.<p>
Over the weeks, that disruption has taken the form of the occupation of buildings on campus, a three-day blockade of campus that ended in mass arrests and daily marches and demonstrations. Though the local news cried for the trustees to have some "backbone" and dismissed the protesters as a "mob," the protesters made up a sizable portion of the 1,800 students enrolled and gained support from deaf communities across the country.<p>
Gallaudet students have shown that sustained pressure can in fact win out when there are no other effective channels through which to voice their concerns.<p>
The struggle, however, is not over. There have been reports of reprisals from the administration, contravening the students' second demand of "no reprisals." Students and faculty will have to maintain pressure to see that this goal is achieved as well.<p>
But the struggle is not over in another regard. "It will have a ripple effect," graduate student Ryan Commerson told demonstrators at the Capitol on October 20, "where people here today can go back to their hometowns tomorrow and fight oppression there, too."<p>

