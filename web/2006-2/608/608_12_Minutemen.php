<font face="Times New Roman, Times, serif" size="4">Day laborers face violence in Laguna Beach, Calif.</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Countering the Minutemen</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 3, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">ON SEPTEMBER 17, several day laborers were attacked at the day laborers' center in Laguna Beach, Calif., by two youths of Russian descent disguised as contractors offering work.<p>
They offered two months of work, for which they promised to pay $3,000 and a kilo of marijuana. When the day laborers rejected their offer, these supposed contractors began to hit them. They broke the nose of one laborer. When another saw his friend was being attacked, he rushed to defend him, causing the racists to flee.<p>
But the two later returned with a vehicle, intending to run over some of the day laborers. One laborer said that he didn't know why, but the car stopped right in front of him, after ramming through a chain-link fence.<p>
A few minutes later, the police arrested the two racists. But by Tuesday, the district attorney had set them free, claiming he hadn't found sufficient evidence to detain them.<p>
This day laborers' center has been under attack by the racist Minuteman Project for more than a year, creating a wave of fear among contractors. Before the attacks, more than 35 contractors per day would hire workers from the site, but that has now been reduced to just 15 or 20. The workers have also been subjected to numerous racist insults such as "Go back to Mexico," yelled from passing cars.<p>
On October 3, a resident of Laguna Beach who is a member of the Minuteman Project filed a suit in the Santa Ana Superior Court demanding that the city stop funding the day laborers' center because all the workers are "illegals."<p>
In support of the lawsuit, the Minutemen planned another rally in front of the center on October 7. But this time, the day laborers were ready for them.<p>
Nativo L&oacute;pez, president of the Mexican American Political Association, met with them the week before, stressing the importance of day laborers playing a leading role in the protests. He also encouraged them to help organize the immigrant rights movement by joining Hermandad Mexicana.<p>
On October 7, the day laborers and their families prepared a delicious meal to share with the activists who came to support them. Unfortunately, only about 10 activists were present to confront the Minutemen, because many organizations on the left still have not prioritized this important struggle. But for the first time, all 50 of the day laborers actively joined the protest, chanting insults and slogans at the Minutemen.<p>
Daniel Vasquez has been getting work through the day laborers' center for two years. "Why do they treat us this way when we're only helping this country?" he said. "The center is a big help to us. We have to win amnesty and become legalized."<p>
When asked why the racist attacks were happening, another day laborer, Francisco, said, "They don't want to get rid of us because we help the economy. It's just a justification to oppress us."<p>
The Minutemen could only muster about 20 protesters, including a Nazi sympathizer with a Confederate flag bumper sticker on his truck. This time, most of the passing cars honked and gave thumbs up in support of the day laborers, not the racists. The Minutemen were forced to leave after just two hours, when their permit expired. The day laborers and activists celebrated a victory, though the struggle is far from over.<p>
This center is the only one of its kind in Orange County, the richest and most conservative county in the United States. It represents the dreams and aspirations of 50 immigrant families. The fight to save the center, and others like it, is a vital part of the immigrant rights movement.<br>
<b>Carlos Morales and Gillian Russom</b>, Los Angeles<p>

