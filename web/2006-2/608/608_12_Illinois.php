<font face="Times New Roman, Times, serif" size="5"><b>An alternative in Illinois elections</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 3, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">IT LOOKS like Chicago voters will actually have some choices come Election Day.<p>
The big story in the election is that the Green candidate for governor, downstate civil rights attorney Rich Whitney, is garnering 14 percent support in some recent polls.<p>
Since the Greens have only a small--if dedicated--core of activists around the state, Whitney's support underlines most of all the disaffection of thousands with the ho-hum "official" choices in the governor's race. In one poll, 51 percent of voters said they weren't happy with either of the main candidates.<p>
The Republicans are running "moderate" State Treasurer Judy Baar Topinka against the pro-business Democratic incumbent Rod Blagojevich. Blagojevich raised the ire of thousands by raiding the state teachers' pension fund, while continuing the local tradition of corruption, with his former top fundraiser, Tony Rezko, being indicted for trying to trade a state pension deal for campaign contributions.<p>
Rich Whitney's penniless campaign seems like an alien force on Illinois' wheel-and-deal political landscape. Whitney opposes the Iraq war and has declared that if he wins, he will order the Illinois National Guard to return from Iraq immediately. He supports full rights for gays and lesbians including marriage.<p>
He has also spoken in support of immigrant rights. Unfortunately, even though Illinois is one of the strongholds of the immigrant rights movement, he hasn't made this a central feature of his campaign. There is nothing on his Web site about immigrant rights or the racist backlash against immigrants.<p>
Worse still, his position on abortion rights mirrors the "legal but rare" position of Democrats like Hillary Clinton. This is in Illinois, which is one of the so-called "trigger" states, where abortion would be immediately outlawed if <i>Roe v. Wade</i> were overturned.<p>
Nevertheless, Whitney's poll numbers show that thousands of Illinois voters want more than the corporate slop being served up by the Democrats and Republicans in Springfield.<p>
There are additionally some local Greens worthy of support. One is Pilsen Green Party activist Dorian Breuer, campaigning for state Senate in Illinois' 1st District. Breuer supports a statewide "big box" ordinance requiring all corporations making more than $1 billion to pay workers $10 an hour plus benefits.<p>
He further supports a state universal health care system and drivers' licenses for immigrants, regardless of status. His Democratic opponent, incumbent Tony Munoz, is a fixture in Richard Daley's Chicago machine, and a member of the Fraternal Order of Police.<p>
Peace activists have also gotten a referendum on the ballot in Cook County. The initiative reads: "Shall the United States Government immediately begin an orderly and rapid withdrawal of all its military personnel from Iraq, beginning with the National Guard and Reserves?"<p>
The wording of the non-binding resolution hedges on the question of "immediate withdrawal" and some supporters of the resolution have argued that the war undermines "our fundamental security" without mentioning the war's impact on Iraqis. It is also unclear as to why National Guard and Reserves should be withdrawn first.<p>
Nevertheless, those who oppose the occupation and support Iraq's right to self-determination should vote "yes." If the initiative wins, it would send a message of dissent following the revelation that the occupation has resulted in more than half a million Iraqi deaths--not to mention the meltdown in Washington over the state of the war.<br>
<b>Adam Turl</b>, Chicago<p>

