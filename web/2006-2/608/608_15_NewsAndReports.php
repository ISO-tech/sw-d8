<font face="Times New Roman, Times, serif" size="4"><b>Stop the attack on immigrants</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CARPENTERSVILLE, Ill.--Members of the Carpentersville Community Alliance (CCA) met with Village President Bill Sarto October 13 to give him their support in opposing the Illegal Alien Immigration Relief Act.<p>
The act would crack down on business owners and landlords who knowingly hire or rent to undocumented immigrants. The proposed legislation also mandates that only English be used in all village businesses.<p>
Some 35 local Latino business owners, community leaders, teachers and ministers formed CCA shortly after the October 3 board meeting where nearly 3,000 people protested the proposed ordinance co-sponsored by two Village trustees, Paul Humpfer and Judy Sigwalt. "We have come together, and we are not going away once this ordinance is voted down," said CCA founder Sylvia Realoza.<p>
Sarto said CCA came to him in order to play a more active role. "A sleeping giant has been awakened," Sarto said. "I encouraged them to get more involved. Forty percent of our population is Hispanic, and there is only one Hispanic on the board."<p>
Earlier in the week, Sarto e-mailed Humpfer and Sigwalt, stating he would remove them from the Village's audit and finance commission if they did not drop the proposed ordinance.<p>
Unfortunately, he has since retracted his threat. This has allowed the two trustees to regroup, and on October 15, 40 people attended the first meeting of the Fox Valley Citizens for Legal Immigration, a group that supports the anti-immigration ordinance.<p>
Humpfer said he would not back down and that he hoped Carpentersville would follow the example of other towns, such as Hazleton, Pa., and Escondido, Calif., that have passed anti-immigrant laws. But the thousands who came out on October 3 to protest against the racist ordinance show that the best way to stop these proposals is through building grassroots opposition.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Protest George Bush</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Lalasz and Matt Larson</b></font><p>

<font face="Times New Roman, Times, serif" size="3">CHICAGO--Nearly 200 people gathered October 12 to protest President George Bush and Speaker of the House Dennis Hastert (R-Ill.) at a fundraiser dinner at the Hilton and Towers.<p>
The demonstration was sponsored by the Chicago Coalition Against War and Racism (CCAWR), HammerHard MediaWorks, International Solidarity Movement-Chicago Chapter, Evanston Neighbors for Peace, Chicago Area Code PINK and the International Socialist Organization.<p>
The protest took place amid police harassment of one of the best-known antiwar activists in the Chicago area, Juan Torres, whose son was killed while on duty in Afghanistan.<p>
On October 10, two days before the anti-Bush protest, Torres opened his home in suburban Schiller Park to Jim Goodnow, a Vietnam veteran and member of Veterans for Peace.<p>
Soon after Goodnow arrived, a Schiller Park police officer appeared and claimed that Goodnow's vehicle, a 40-foot Eagle bus, could not be parked on the street. The bus has the slogans "Bring the troops home now," "For what noble cause? Not one more!" and "Don't attack Iran" on it.<p>
The harassment continued the following day, when police wrote Goodnow a ticket for parking a commercial vehicle in a residential area, even though the vehicle is registered in Texas as a motor home. Hours later, two more squad cars pulled up to the residence.<p>
One of the officers claimed that police had received reports claiming intimidation by Goodnow and complaints about a vehicle "that had anti-American slogans on it and that had slogans supporting Saddam Hussein." The police acknowledged that the report was false--but later, yet another officer appeared at Torres's home and threatened to have the vehicle immediately towed.<p>
Goodnow then called Schiller Park City Hall, and was told that he had until the morning of October 12 to move the vehicle. "We didn't do anything, but a lot of police are tied to the military and they were very angry with what is written on the side of the bus," Torres told <i>Socialist Worker. </i>"But I'm not afraid of them. I've been to Afghanistan, to the middle of a war."<p>
As Goodnow said, "What exactly are the freedoms that our men and women are dying for in the Middle East, when here at home, our own freedoms are being stripped away?" Goodnow did drive his bus past the Bush protest at the Hilton, and was greeted by a loud chant from the crowd of "bring them home now!"<p>

