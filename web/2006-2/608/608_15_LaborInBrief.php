<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">November 3, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Los Angeles Unified School District</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By David Rapkin, UTLA</b></font><p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES--Teachers returned to classes this fall to find that one of our most popular and successful rank-and-file union leaders had been involuntarily transferred from his school by Superintendent Roy Romer and the Los Angeles Unified School District (LAUSD).<p>
The charges leveled against Alex Caputo-Pearl, a 10-year history teacher and Chapter Chair at Crenshaw High School, were absurd on their face. Despite the fact that he is tremendously popular with students, teachers and parents, LAUSD said that Alex was "lowering morale," was unable to "get along with the school's principal," and was an impediment to the accreditation process the school is undergoing.<p>
In reality, LAUSD officials had themselves praised Alex--in writing--for his positive role at the school. He had worked amicably with several principals. And when the District needed to help other schools retain their accreditation, they actually turned to Alex to help train teachers and administrators in the process.<p>
To remove Alex, the District made use of a devastating clause in the United Teachers Los Angeles (UTLA) contract that allows administrators to transfer teachers whenever it "is deemed to be in the best interest of the educational program of the District." Naturally, our bosses have used this vague clause hundreds of times to punish activists, almost always with impunity.<p>
Not this time. A series of escalating actions by teachers, parents and students forced LAUSD to re-instate Alex and shamed them into admitting that they had made a serious mistake.<p>
Key to the victory was the organization Alex and his co-workers had built. Over several years, the teachers, parents and students at Crenshaw, the major high school in the historically Black Crenshaw district of LA, organized themselves into the Crenshaw Cougar Coalition, a powerful, multi-racial group that brought broad sections of the community together to fight for better conditions at Crenshaw.<p>
In addition, UTLA activists, many of them members of the union reform movement, Progressive Educators for Action (PEAC), were key to turning hundreds of teachers out to school board meetings to protest and testify against the transfer. PEAC argued that we needed to draw a line in the sand around this case, since the District was clearly testing our strength as a union by targeting Alex, of all people.<p>
Many teachers, parents and students learned the lesson from this struggle that when we fight, we can win. At the press conference celebrating Alex's return to Crenshaw, student Frances Martin was defiant. "They tried to attack us," said Frances. "And we didn't stand for it. Try to take one of our teachers again!"<p>
Throughout the fight, Alex stressed that the fight was about far more than his case. At the press conference, he listed the "real issues" we had forced into the open, including "reducing class size; retaining top-flight teachers in the inner city; bringing much-needed social services back to our communities; and righting the historic wrongs of racism."<p>
As a result of this victory, UTLA leaders have added a new demand to our contract fight--no involuntary transfers of activists without due process. Now that we've tasted our power, the fight to "right the historic wrongs" that plague our schools continues.<p>

<font face="Times New Roman, Times, serif" size="4"><b>Westin Hotel</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Steve Leigh</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SEATTLE--Some 200 members and supporters of Hotel Employees and Restaurant Employees (HERE) Local 8 picketed the Westin Hotel October 24 to demand a new contract.<p>
The old contract expired in May, and negotiations have ground to a halt. Management is demanding concessions, including a wage freeze, cutbacks in hours at its discretion and possible subcontracting.<p>
Employees were vocal and determined. Chanting "We're fired up, won't take no more!" and "Westin, Westin, you can't hide, we see your greedy side!" workers picketed for two hours. During the rally, long-time workers from around the world spoke--from China, the Philippines, Latin America and elsewhere.<p>
"We make this hotel run!" said one of the speakers. "Without us, there would be no hotel." "I came to this country from the Philippines to have a better life," said another. "I'm not going to let hotel management take that away from me!"<p>
At the end of the rally, 24 members and supporters of HERE blocked the road in front of the Westin in an act of civil disobedience. Traffic slowed as police made arrests while union members chanted, "Let them go" and "We'll be back!"<p>
Workers hope that the rally and civil disobedience will pressure management into coming back to the bargaining table--especially after a worker fired for theft recently got her job back after a picket and confrontation with management. They know that the Westin, a wealthy multinational corporation, can afford a decent contract.<p>

