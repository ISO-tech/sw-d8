<font face="Times New Roman, Times, serif" size="5"><b>Victim of Israeli torture on trial</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 3, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">FEDERAL PROSECUTORS are attempting to smear Mohammed Salah as a Palestinian "terrorist"--while ignoring Israeli atrocities committed against Salah himself, among so many others.<p>
Salah is a Palestinian activist who was arrested in 2003 for supposedly providing "material support" to the militant Palestinian group Hamas, which the U.S. government considers a terrorist organization.<p>
Salah and his lawyer Michael Deutsch say that he is guilty of nothing more than donating humanitarian aid to Palestinian refugees, but prosecutors say the money was used to fund attacks on Israel and claim the case against him is part of the "war on terror."<p>
To that end, prosecutors are putting not just Salah on trial, but Hamas itself. The prosecution has introduced posters showing Hamas suicide bombers superimposed next to Hamas politicians.<p>
Last week, U.S Treasury Department official Matthew Levitt outlined more than three dozen acts of terrorism supposedly committed by Hamas against Israel between 1992 and 2004. The prosecution admits that Salah was in an Israeli jail for much of that time, but their case hinges on "guilt by association."<p>
Levitt testified that Hamas suicide bombings were an attempt to "undermine Israeli sense of security. It sends a message that Israelis aren't safe anywhere in the country."<p>
But as Deutsch pointed out on cross-examination, Levitt's emphasis on Israeli casualties obscures the reality of daily life for Palestinians. Deutsch pointed out that, according to one study, approximately 1,400 Israeli soldiers and civilians have been killed since 1987, compared to more than 5,500 Palestinians killed. "Are you not interested in the fact that Palestinians, unarmed Palestinian people, are killed at a rate of five times the number of Israelis killed?" Deutsch asked Levitt.<p>
Prosecutors claim that Salah admitted to delivering money and recruits to Hamas. But that supposed "admission" was obtained in Israel, where Salah spent four-and-a-half years in jail in the mid-1990s, following what he says was torture--including days of hooding, sleep deprivation and death threats--by his Israeli captors.<p>
Upcoming testimony in the trial is expected to include two of Salah's Israeli captors, who will be allowed to testify anonymously.<p>
While the Bush administration ratcheted up the witch-hunt against Salah, it was the Clinton administration that invoked special terrorism laws in 1995 to label Salah, a U.S. citizen, a "specially designated terrorist."<p>
According to law professor David Cole, when Salah was placed on this list, it became a crime for anyone in the U.S. to have any economic transactions with him.<p>
"As a result," Cole wrote in Britain's <i>Guardian</i> newspaper, "Salah was subjected to a kind of internal banishment, in which it was a crime for anyone else in the United States to provide him with anything of value. The law prohibited Salah's neighbor from offering him a cup of coffee, the local news agent from selling him a newspaper, or a doctor from treating him in the hospital. The Treasury Department told him he could get a job only if he first informed his prospective employer that he was a 'specially designated terrorist' and the employer then obtained a special license to pay Salah."<p>

