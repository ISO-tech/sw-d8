<font face="Times New Roman, Times, serif" size="4">After N.J. decision on gay marriage, the right goes on...</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Playing politics with equal marriage rights</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 3, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">IN A welcome, though partial, blow against marriage discrimination, the New Jersey Supreme Court ruled October 25 that same-sex couples have a constitutional right to the same state benefits, protections and obligations as all married couples.<p>
The 4-3 decision stopped short of legalizing same-sex marriage. The three dissenting justices said the ruling didn't go far enough, and that only marriage would enable gay couples to achieve equal benefits. Now the state legislature has 180 days to come up with a plan to remedy discrimination against same-sex couples, but it could allow civil unions instead of full marriage rights.<p>
The decision comes on the heels of several setbacks that followed the first historic victories in the struggle for equal marriage rights.<p>
In 2003, the Massachusetts Supreme Court made gay marriage legal, making Massachusetts the first and only state to do so. The next year, lesbian and gay activists were celebrating San Francisco Mayor Gavin Newsom's decision to award out marriage licenses, with couples lining up by the hundreds at the courthouse steps.<p>
Since then, well-financed players from the antigay Religious Right have tried to turn back these important victories. Twenty states have passed constitutional amendments banning same-sex marriage, and George Bush announced his desire for a federal Constitutional ban.<p>
Lately, however, support for the antigay marriage crusade has waned. For example, "Stand for the Family" rallies organized by Focus on the Family founder James Dobson over the last few months have had pathetic attendance. In Pittsburgh in September, only 3,000 people turned out to a 17,000-seat venue for a Focus on the Family event, according to the <i>Washington Post.<p>
</i>Some of the hate-mongers believe the New Jersey decision will compel the right to come out to the polls. "I have to think there are Democratic strategists out there thinking the words of the old Japanese admiral: 'I fear all we've done is wake a sleeping giant,'" Tony Perkins, president of the Family Research Council, told the <i>Post.</i> "They were coasting into an election with a Republican base with dampened enthusiasm. This brings it all back home to the base, what this election is about."<p>
Bush was quick to decry the New Jersey ruling at an Iowa fundraiser. "Yesterday in New Jersey, we had another activist court issue a ruling that raises doubts about the institution of marriage," he said. "I believe it's a sacred institution that is critical to the health of our society and the well-being of families, and it must be defended."<p>
There are constitutional amendments banning same-sex marriage on the November 7 ballot in eight states--Arizona, Colorado, Idaho, South Carolina, South Dakota, Tennessee, Virginia and Wisconsin.<p>
But this is just one side of a tremendously polarized issue. A February Zogby poll showed that a majority in New Jersey favored marriage for gay couples--by a margin of 56 percent to 39 percent.<p>
Activists should oppose each and every antigay marriage measure on the ballot in November.<p>
Looking beyond the narrow framework of what's on offer in the elections--the allegedly pro-gay Democrats v. antigay Republicans--is also critical.<p>
A single-minded focus on electing Democrats is in large part to blame for the disintegration of the equal marriage movement. As the struggle was getting underway in 2004, supporters were told to focus on the candidates rather than protests that might embarrass them.<p>
We can't let the politicians play politics with our lives. Here are facts: Gay and lesbian couples, because they cannot be legally married, are denied more than 1,000 rights and protections awarded other married people--such as child custody rights or the right to make life-and-death decisions for their partner.<p>
If the New Jersey legislature doesn't take the next step and pass a law granting equal marriage rights, same-sex couples will continue to be relegated to second-class citizenship. In Vermont, a similar court decision in 1999 resulted in a compromise civil union law.<p>
We have to tell New Jersey legislators that they can't stop short with this partial blow against discrimination and bigotry in New Jersey.<p>

