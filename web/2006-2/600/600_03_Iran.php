<font face="Times New Roman, Times, serif" size="4">U.S. escalates pressure on Iran</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Is the next war drive underway?</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 8, 2006 | Pages 1 and 3</font><p>

<font face="Times New Roman, Times, serif" size="3">THE BUSH administration is again ratcheting up threats against Iran for refusing to halt its nuclear program.<p>
Bush unleashed a new torrent of abuse in a recent speech at the American Legion national convention in Salt Lake City. The recent war in Lebanon and Iran's support for Hezbollah, Bush said, "made it clearer than ever that the world now faces a grave threat from the radical regime in Iran...We know the death and suffering that Iran's sponsorship of terrorists has brought, and we can imagine how much worse it would be if Iran were allowed to acquire nuclear weapons."<p>
At issue is Iran's nuclear enrichment program. The Bush administration claims Iran is attempting to produce nuclear weapons, but the government of Iranian President Mahmoud Ahmadinejad says its program is for energy production. Iran's plans to enrich uranium don't violate the terms of the Nuclear Non-Proliferation Treaty (NPT).<p>
Still, that hasn't stopped the Bush administration from pushing the United Nations (UN) to bring sanctions against the country to restrict travel by Iran's leaders and limit the country's access to financial markets.<p>
Since it's expected that the U.S. will have a difficult time getting harsh sanctions approved by Russia (which is building an $800 million nuclear plant in southern Iran) and China (which views Iranian oil and natural gas as vital to its economy), the Bush administration has also been taking matters into its own hands, putting pressure directly on European banks to stop lending to Iran.<p>
The administration's push for sanctions comes even as a new report from the International Atomic Energy Agency "confirmed that Iran had continued producing enriched uranium, but only on a small scale and at relatively low levels" according to the <i>New York Times</i>. The report added, "Inspectors have not uncovered any concrete proof that Iran's nuclear program is of a military nature."<p>
Even Pentagon sources recently admitted to the <i>Washington Times</i> that Iran is at least five years away from being able to build its first nuclear weapon. But a section of the U.S. political establishment is pushing even harder for a "pre-emptive" war with Iran.<p>
Some of the Bush administration's "neo-con" supporters were up in arms, for example, at a visit to the U.S. by former Iranian President Mohammad Khatami, leader of the reformist wing of the Iranian establishment. "Giving Khatami prestigious platforms all over America is a dumb move, and it will enormously discourage the Iranian people," Michael Ledeen of the American Enterprise Institute wrote in the <i>National Review Online</i>.<p>
According to investigative journalist Seymour Hersh, some administration insiders believe provoking a war with Iran could help salvage the downward spiral of the U.S. occupation in Iraq. <p>
"[S]ome conservatives are arguing that America's position in Iraq would improve if Iran chose to retaliate there, according to a government consultant with close ties to the Pentagon's civilian leaders, because Iranian interference would divide the Shiites into pro- and anti-Iranian camps, and unify the Kurds and the Sunnis," wrote Hersh in July in the <i>New Yorker</i>.<p>
With the U.S. and Israel also blaming Iran for supporting and arming Hezbollah, the recent failure of Israel to achieve its objectives in Lebanon has added fuel to the drive for a future attack against Iran, not only in the U.S., but in Israel as well--the one country in the Middle East known to actually possess illegal nuclear weapons, and which has refused to sign onto the NPT.<p>
According to Britain's <i>Sunday Times</i>, "The conflict with Hezbollah has led to a strategic rethink in Israel...The challenge from Iran and Syria is now top of the Israeli defense agenda, higher than the Palestinian one,' said an Israeli defense source. Shortly before the war in Lebanon Major Gen. Eliezer Shkedi, the commander of the air force, was placed in charge of the 'Iranian front,' a new position in the Israel Defense Force. His job will be to command any future strikes on Iran and Syria."<p>
We have to stand up against the Bush war drive against Iran.<p>

