<font face="Times New Roman, Times, serif" size="5"><b>Victory for Josh Wolf</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 8, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>Report by Carlos Villarreal, executive director of the National Lawyers Guild, San Francisco Bay Area</b><p>

A 24-YEAR-OLD independent journalist was released on bail September 1 from a Northern California federal prison after serving a month for contempt after refusing to hand over his unpublished video footage of a 2005 protest.<p>
The footage was subpoenaed by a San Francisco federal grand jury claiming that it was investigating a possible attempt to burn a police car, but as with most of the federal government's "law enforcement" activities, it is more likely an attack on civil liberties and part of a broader political agenda.<p>
Josh Wolf was represented by members of the National Lawyers Guild in federal district court, who attempted to argue that more protective state law should prevail and that the government's request was unnecessary and a violation of Wolf's First, Fourth and Fifth Amendment rights.<p>
In July 2005, dozens of activists marched in San Francisco's Mission District in solidarity with G8 Summit protests in Scotland. The protest involved minor property damage--to newspaper racks, a KFC, a bank and one other business--but more significantly an injury to a police officer. No one has been charged with assaulting the officer, and the federal government does not claim to be investigating how the officer was injured.<p>
Not widely publicized is the fact that the officer was beating protesters with his baton while his partner was choking another protester in a head lock, or that the two had recklessly driven their police car into the crowd. In the middle of all of this was Wolf filming.<p>
Because of the injured officer, local television stations used his footage without his permission and repeated the SFPD story that it was a violent protest and an officer was ambushed.<p>
FBI agents with SFPD officers visited Josh on two occasions, and eventually he was ordered to appear before a federal grand jury and hand over his unedited material. The Feds say such a crime falls under their jurisdiction because SFPD cars are paid for, in part, with federal dollars.<p>
Historically, grand juries are used against political activists, people of color and anyone the Feds have it out for, when they know they don't have enough evidence to file charges in court or get legitimate search warrants.<p>
In Wolf's case, he is a self-described "anarchist" whose beat is left-wing activists of all stripes. If the government could get their hands on all his materials, they would. And who knows what sorts of trumped up charges they might prepare against anyone appearing in his videos.<p>
While the same prosecutors and police do not appear to be spending a lot of time investigating violent anti-abortionists, white supremacists or corporate looters, Wolf--a nonviolent journalist--was locked up.<p>
Depending on a decision on the merits from the Ninth Circuit Court of Appeals, Wolf may have to return to prison and could remain there through the life of the grand jury--which is set to expire next summer but could be extended.<p>
