<font face="Times New Roman, Times, serif" size="4">Bankruptcy judge stops flight attendants' walkout</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Strike barred at Northwest</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 8, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">A FEDERAL bankruptcy judge has barred flight attendants from striking at Northwest Airlines, handing management a new weapon in its attack on its unions. Management forced a strike with mechanics a year ago that is still ongoing.<p>
The temporary stay on a strike by the 9,000 Northwest workers, represented by the Association of Flight Attendants (AFA), issued by U.S. District Judge Victor Marrero, bans the union from implementing its strategy of scattered walkouts and job actions, known as Create Havoc Around Our System (CHAOS).<p>
The AFA claimed that Northwest's move in July to impose a 21 percent wage cut on flight attendants gave the union the legal right to strike. Flight attendants had earlier voted down tentative agreements with concessions worth $195 million.<p>
Other unions at Northwest have already agreed to substantial givebacks, including the Air Line Pilots Association (ALPA) and the International Association of Machinists (IAM).<p>
The flight attendants, like members of the other unions at Northwest, themselves undermined labor solidarity a year ago by crossing the picket lines of the striking Air Mechanics Fraternal Association (AMFA), which is independent of the AFL-CIO.<p>
Northwest had demanded pay cuts of 26 percent and the outsourcing of 2,000 jobs. When the mechanics refused, Northwest was ready with scabs. Next, Northwest declared bankruptcy, and management presented draconian demands for cuts to other unions as well.<p>
Members of ALPA and the IAM also authorized strikes, but kept crossing AMFA's picket lines and ultimately voted to accept concessions--$190 million for the IAM, and a 39 percent pay cut for pilots, worth $358 million. Members of both unions then voted out of office the union local presidents who negotiated the givebacks.<p>
The flight attendants have had even greater internal splits. They voted to decertify the International Brotherhood of Teamsters in 2003 in favor of an independent union after the Teamsters refused to stand up to demands for concessions.<p>
When the flight attendants' independent union couldn't make progress, the AFA moved in, winning bargaining rights this year. But the AFA, which is affiliated with the Communications Workers of America, has itself presented two concessionary tentative agreements that workers voted down.<p>
The lesson is clear enough: having failed to hang together with the striking mechanics, the unions at Northwest are hanging separately. A strike by flight attendants is still possible--and could be a step toward rebuilding union power at Northwest.<p>

