<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">September 8, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Gary, Ind., teachers</b></font><p>

<font face="Times New Roman, Times, serif" size="3">GARY, Ind.--Following a nearly two-week-long illegal strike, public school teachers unanimously approved a tentative contract agreement with the Gary Community School Corporation (GCSC) and returned to work September 1.<p>
Unfortunately, Gary Teachers' Union president Sandra Irons' comment to the Associated Press--"I'm glad it's over. Let's go to work"--illustrates just how rotten a deal the union was forced to swallow.<p>
On all main issues on the table, the GCSC gained the upper hand. Teachers will receive a salary increase of 2 percent each year over the life of the three-year contract, but much of this will be eaten up by an increase in health care co-pays. Furthermore, the raise is retroactive only to last year, even though teachers have been working without a contract since December 2004.<p>
The school year will be extended in June so that teachers can make up class time missed due to the strike. Finally, although better than the 30-minute lunch breaks the GCSC had sought, the agreement would reduce lunch breaks from one hour to 45 minutes.<p>
The union apparently folded under relentless pressure from the GCSC, which threatened to fire all 1,600 strikers if they did not return to work immediately.<p>
The long-term impact of this strike on the Gary Teachers' Union remains to be seen. Union president Irons had billed the fight as a "power struggle" in which the school board was attempting to destroy the union. And although the contract itself is a loss, the strike--their first in 22 years--has demonstrated the union's capacity to advance on some key strategic fronts. <p>
In its first week, teachers forced schools to close despite the board's scabbing campaign, and maintained almost 100 percent turnout on picket lines in the face of the media's claim that up to 40 percent of teachers would report to work. The teachers also won widespread support from parents and other unions.<p>
To win gains in future contracts, the union must deepen these connections to labor and the community--and, if necessary, be prepared to stay out on strike longer.<p>

