<font face="Times New Roman, Times, serif" size="4">Organizing drive at Smithfield Foods</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Workers need to learn their rights"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">September 8, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">SINCE 1993, the United Food and Commercial Workers (UFCW) union has been trying to unionize Smithfield Foods' Tar Heel, N.C., plant--the largest hog processing plant in the world.<p>
Workers have faced intimidation, suppression of their rights, racism on the job and--at one point--an armed company police force.<p>
Each day, the plant's 5,500 workers--nearly 80 percent of them immigrants--process some 32,000 hogs. The work is fast, and dangerous: the accident rate at the Tar Heel plant is among the highest in the country. Smithfield has been cited by Human Rights Watch for violating international human rights standards. <p>
EMMA HERRERA is executive director of the Eastern North Carolina Workers Center in Red Springs, N.C., run by the UFCW. She spoke with BEN LASSITER about the struggle for justice at Smithfield.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>WHAT KIND of issues do workers face at the Smithfield packing plant?</b><p>

THEY HAVE issues about injuries, discrimination, sexual harassment, verbal abuse, physical abuse--you name the harassment and they have it. <p>
We have a lot of different worker committees inside Smithfield. Right now, they're sending letters to the company, and they recently used the union's yellow T-shirts to publicize the union.<p>

<b>I'VE HEARD that Smithfield divides up workers inside the plant based on race--Latino, Blacks, Lumbee Indians, whites. Is that true?<p></b>

YES, It's true. They want to pit the African Americans against the Latinos, and the Latinos against the Lumbee Indians. If the company sees the Black and Latino workers together, then they know that they have a union, so they try and separate them. That's the point.<p>
Right now, we have to finish educating the workers--workers need to learn their rights. They have the right to talk about unions in the break areas, the parking lots. We live in a country with freedom of speech, and workers need to have this freedom.<p>

<b>WHAT'S THE connection between the labor movement and the immigrant rights movement?</b><p>

THEY ARE connected--definitely. If you're talk about immigration, you're talking about workers. If you're talking about workers, you're talking about immigration. <p>
The May 1 march was in Lumberton, N.C., and we had about 5,000 to 6,000 workers marching. We affected every single company around this area, and we closed these companies.<p>
