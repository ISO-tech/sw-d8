<font face="Times New Roman, Times, serif" size="5"><b>His crime was wearing a T-shirt</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 8, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">"WE WILL Not Be Silent." These were the words so offensive that airport personnel forced Raed Jarrar to cover up his T-shirt.<p>
The Arab-American human rights activist of Iraqi descent, who had recently visited the Middle East, was on his way home to California August 12 when he was stopped in John F. Kennedy Airport in New York and told that several passengers had complained about his shirt. <p>
Jarrar's black T-shirt had the slogan "We Will Not Be Silent"--adopted from a slogan of resistance against Nazi Germany--printed in Arabic and English. The Global Exchange activist had already passed through two security checks, but two men asked to see his ID and boarding pass.<p>
When asked to change his shirt, Jarrar said that he had no idea that the shirt was going to be offensive, and refused to change it since it was within his rights to wear it. "I grew up and spent all my life living under authoritarian regimes and I know that these things happen," Jarrar told WNYC radio. "But I'm shocked that they happened to me here, in the U.S."<p>
Jarrar also said that one official told him, "Going to an airport with a T-shirt in Arabic script is like going to a bank and wearing a T-shirt that says, 'I'm a robber.'" <p>
A JetBlue representative offered to get him another shirt to put over his, and Jarrar agreed since it looked like he might miss his plane if he refused. As he was about to board the plane, Jarrar was informed that the airline had switched his seat to the back of the plane.<p>
"It sucks to be an Arab/Muslim living in the U.S. these days," he wrote on his blog <i><a href="http://raedinthemiddle.blogspot.com" target="_blank">Raed in the Middle</a></i>. "When you go to the Middle East, you are a U.S. taxpayer destroying people's houses with your money, and when you come back to the U.S., you are a suspected terrorist and plane hijacker."<p>
Since the incident, <i>Democracy Now! </i>reports that activists around the country are wearing the "offending" T-shirt in airports in protest of this racist violation of civil liberties. We will not be silent. <p>
<i>To ask JetBlue about its regulations against Arabic script and other violations of constitutional right, call 800-JETBLUE (538-2583) or go to the <a href="http://www.jetblue.com/speakup/complaintsform.aspx" target="_blank">complaint form</a> on the JetBlue Web site</i>.<p>

