<font face="Times New Roman, Times, serif" size="5"><b>Israel--aiding the world's butchers</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | September 8, 2006 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3">BURIED IN the final paragraphs of an article about the discovery of what are probably the remains of Colombian paramilitary death squad leader Carlos Casta&ntilde;o, the author mentions in passing that Casta&ntilde;o "took arms defense training" in Israel.<p>
This is the kind of selective reporting that is the hallmark of the "free" press. In the wake of Israel's invasion and devastation of Lebanon, you might think that a little tidbit like this would receive more prominence--perhaps a headline, "Colombian death squad leader was trained by Israelis." But in the bourgeois media, as in war, truth is always the first casualty, often buried alive at the bottom of stories rather than killed outright.<p>
In his autobiography, Casta&ntilde;o says that he spent a year in military training in Israel in 1983. "I copied the concept of paramilitary forces from the Israelis," he wrote.<p>
In the late 1980s, Casta&ntilde;o was also the star pupil of retired Israeli army colonel Yair Klein, who had been brought into Colombia by Pablo Escobar's infamous Medell&iacute;n drug cartel to train its death squads. Casta&ntilde;o ran death squads under Escobar, and then under the Cali cartel, whose aim was to fight Colombia's guerrilla insurgency and protect the interest of Colombia's drug traffickers and big cattle ranchers.<p>
In 1997, Casta&ntilde;o organized Colombia's death squads into a single umbrella organization, the United Defense Groups of Colombia (AUC).<p>
Before his death, Casta&ntilde;o had 27 arrest warrants hanging over his head for charges involving massacres, kidnapping, assassination and drug trafficking. Closely tied to Colombia's military, the death squads have the financial support of drug traffickers, ranchers and Colombian businesses. They have been used not only to try and root out guerrilla organizations like the FARC but also to systematically murder labor, peasant and human rights activists.<p>
According to Garry Leech, writing in the <i>Colombia Journal, </i>"AUC fighters routinely induced fear in the rural population by entering villages and rounding up the residents in the town plaza. They would then brutally kill a handful of villagers, often dismembering them with machetes and chainsaws, before ordering the rest of the people to leave the region."<p>
It had been rumored that Casta&ntilde;o was hiding in Israel to avoid extradition to the U.S. on drug trafficking charges. It should not be surprising that anyone suspected this.<p>
Israel has a sordid history as an arms trader and military trainer for unsavory dictatorships and death squads throughout the world, and has acted closely with the U.S. to suppress left-wing and nationalist movements that threatened U.S. interests throughout the world.<p>
"Israel operates worldwide as a subcontractor of the United States," wrote an Israeli newspaper in 1986, "in countries where the United States cannot operate directly."<p>
This role became especially important after the U.S. debacle in Vietnam, when the U.S. felt more reticent to involve its own forces directly in other countries. According to Lance Selfa writing in the <i>International Socialist Review, </i>"Israeli military advisers helped to train the militaries and secret police agencies of such 'friends of Israel' as the Shah of Iran, Mobutu Sese Seko in Zaire, Emperor Bokassa in the Central African Republic, General Idi Amin in Uganda, and Ian Smith of Rhodesia."<p>
Violating international embargoes, Israel armed and trained South African commandos to fight Black freedom fighters. Israel sold weapons to South American military dictatorships in Brazil, Chile and Argentina in the late 1970s and early 1980s.<p>
In Central America, Israel provided the lion's share of weapons and military training to dictatorships in El Salvador, Guatemala, Honduras and Nicaragua, including the training of death squads.<p>
Israeli technique and hardware helped the Guatemalan regime conduct military sweeps of Mayan communities that killed tens of thousands of people. After the fall of the Somoza dictatorship in Nicaragua, Israel helped arm and train the counterrevolutionary army, the contras, in Honduras, which aimed to overthrow the Sandinista government.<p>
No wonder Major General George Keenan once described Israel as being "worth five CIAs."<p>

