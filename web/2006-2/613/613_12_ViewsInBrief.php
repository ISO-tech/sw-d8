<font face="Times New Roman, Times, serif" size="4"><b>A job selling lies to teens</b></font><p>

<font face="Times New Roman, Times, serif" size="3">RECRUITER CHRISTOPHER Treichel will have to excuse me if I don't shed a tear over the fact that antiwar activists are making his job filling the ranks "so difficult" these days ("Wrong on recruitment?" December 8).<p>
Treichel claims that we are "misled and misinformed," but in truth, that's an excellent way to describe young people who find themselves being lured into signing away their futures to the military by people like him.<p>
Recently, WABC television sent several students, equipped with hidden video cameras, into 10 Army recruitment offices in New York, New Jersey and Connecticut. What they found was a systematic pattern of recruiters lying to students in order to get them to join.<p>
One student was told by a recruiter, "We're not at war. War ended a long time ago." Another recruiter told a different student that the chances of going to war "would be slim to none." Other students were promised that they could simply walk away from the military if they chan ged their minds after joining. And still others had recruiters offering to coach them on how to avoid failing drug tests.<p>
Treichel claims that recruiters don't target the poor. Yet in 1995, Tom Wilson, a high-level official in charge of the Army's personnel department, said in an interview that the military targeted students "particularly in inner cities...I hesitate to use the term at-risk kids, but kids who would otherwise be called at-risk."<p>
As for Treichel's claim that "college money is an absolute" for recruits, maybe <i>he </i>should do his homework. According to the Central Committee for Conscientious Objectors, "Two-thirds of all recruits never get any college funding from the military. Only 15 percent graduated with a four-year degree."<p>
The Veterans Administration estimates that one-third of homeless people in the U.S. are veterans. But perhaps Treichel thinks they simply squandered away all the fabulous "opportunities" the military provided.<br>
<b>Nicole Colson</b>, Chicago<p>

