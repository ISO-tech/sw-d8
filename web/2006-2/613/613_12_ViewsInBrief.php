<p><a name="Reds"></a><font face="Times New Roman, Times, serif" size="4"><b><i>Reds</i> released on DVD</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BEING THAT I'm locked up, I'm often behind on the news, so you folks may already know this: I just read that the 1981 film <i>Reds</i> was released on DVD.<p>
I'm sure you folks are familiar with the movie, but if not, it's a biopic of John Reed, starring Warren Beatty and Diane Keaton. I think the recent (and overdue!) release of this film on DVD is very worthy of a review. I would offer to review it myself, but it's been more than five years since I've seen it.<p>
I saw it before I was a Marxist, and so I looked at it differently then. It did prompt me to buy Reed's famous book <i>Ten Days that Shook the World</i>, which ultimately led me to read more about Marxism. What I recall from the movie was that Trotsky seemed to be portrayed as a manipulative nut, and, of course, that Bolshevism led to Stalinism. I definitely remember walking away with that thought, though I was taken with events enough that I read more on the subject.<p>
I now have a much better grasp of Marxism and the Russian Revolution. I cannot wait to watch <i>Reds</i> again next year when I'm released, now with a different perspective.<br>
<b>Christopher Henderson</b>, Low Security Correctional Institution, Butner, N.C.<br>

