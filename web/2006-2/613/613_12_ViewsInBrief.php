<p><a name="SW"></a><font face="Times New Roman, Times, serif" size="4"><b>Too hard on <i>SW's </i>review</b></font><p>

<font face="Times New Roman, Times, serif" size="3">IN RESPONSE to Alessandro Tinonga's letter "Nothing funny about <i>Borat</i>" (December 1), it seems the question at hand is, "Is <i>Borat</i> a predominantly racist and backwards movie?" I am in agreement with Brian Jones in his review with an emphatic "no."<p>
I think Alessandro is throwing the baby out with the bathwater and mischaracterizes Jones' review along the way. No one is saying <i>Borat</i> is a scathing attack on bigotry and should be held up as the most progressive movie since <i>Salt of the Earth.</i> And no one, especially Brian Jones, is saying that we should keep our mouths shut about the film's shortcomings.<p>
But scenes such as Borat's speech to the rodeo crowd, where he clearly exposes the racism behind the war in Iraq, should be applauded. Other scenes, revealing the everyday bigotry that exists just under the surface of American society, should be encouraged.<p>
Instead, Alessandro instructs people who want a good laugh <i>and</i> social justice to read "next to a roaring fire of Borat DVDs."<p>
The role of socialists should be to raise the level of working-class consciousness to that of the most class-conscious in society. To turn mixed and uneven consciousness into class consciousness, socialists and progressives need to be able to relate to the good ideas, generalize them and fight against the bad ones. We can only do this in struggle, alongside people with all sorts of ideas about society and culture.<p>
Calling for the incineration (a wildly reactionary image) and boycott of the most popular comedy in America--one that does have progressive points and messages within it--accomplishes none of this, and comes off as pedantic and condescending.<br>
<b>Brian Lenzo</b>, Rochester, N.Y.<p>

