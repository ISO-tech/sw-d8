<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">December 15, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Macy's</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Todd Chretien and Mer Stevens</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Stationary Engineers Local 39 picketed Macy's San Francisco stores as part of a strike throughout Northern California that started just before Thanksgiving.<p>
"Macy's wants to cut workers' cost of living increase in half and institute a two-tier health care system," union member Joe Klein told <i>Socialist Worker.<p>
</i>Despite strong profits, Macy's management offered the engineers a raise of only 6 percent over three years, which is far below the skyrocketing cost of living in the San Francisco Bay Area. This effective pay cut would leave Macy's engineers' salaries an average of 15 percent lower than workers doing the same jobs at other large buildings in San Francisco.<p>
"The want us to fight each other for a little piece of cheese," said Klein. "We're standing together instead." Klein said that 50 Macy's stores from San Francisco to Reno are out on strike.<p>
"Apparently Macy's is trying to go nonunion," according to a statement by Local 39's business manager Jerry Kalmar. "What a shame. They asked for a strike and they have one. This could be a long strike and we need your help."<p>

<font face="Times New Roman, Times, serif" size="4"><b>Committee on Temporary Shelter</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jim Ramey</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BURLINGTON, Vt.--Workers at the Committee on Temporary Shelter (COTS) in Burlington have voted in favor of unionizing 31-2. The vote took place after weeks of negotiating with the board of COTS and not being able to reach a settlement.<p>
The board claims that unions aren't necessary for nonprofit workers--and that a union would force dire cutbacks in the program. Two days after the overwhelming vote to unionize, the board unanimously rejected the election. The board claims that "substitutes," or part-time workers, are ineligible for the union, as well as managers who have "supervisory or confidential duties" that meant they could not be part of a bargaining unit.<p>
Organizers for the United Electrical, Radio and Machine Workers of America (UE), who represent the COTS workers, have not backed down. UE has organized a solid workplace--which could be very significant in a town with many nonprofit organizations.<p>
"The only issue is if they respect workers' rights," said Kimberly Lawson of UE International in the <i>Burlington Free Press</i>. "If they don't, the workers are prepared to make our case in the community."<p>
Importantly, the workers and UE organizers have refused management demands for a National Labor Relations Board election process on the grounds that the labor board is hostile to workers. Workers will meet December 13 to discuss further actions.<p>

