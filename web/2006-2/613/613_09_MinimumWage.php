<font face="Times New Roman, Times, serif" size="4">Strong support for raising the minimum wage</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Working for the minimum</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 15, 2006 | Page 9</font><p>

<font face="Times New Roman, Times, serif" size="3"><b>HADAS THIER reports on the battle over the minimum wage.</b><p>

THIS JANUARY, the minimum wage will rise in six states--Arizona, Colorado, Missouri, Montana, Nevada and Ohio--as a result of successful ballot initiatives that passed overwhelmingly in November.<p>
Another three states will raise their minimum wages as a result of legislative action over the past year. That will bring the number of states with minimum wages above the abysmally low federal level of $5.15 an hour to 28, plus the District of Columbia.<p>
State minimum wages above the federal level will range from $6.15 an hour to $7.94 in Washington state. What's more, the successful referendums included clauses to peg the minimum wage to cost-of-living increases.<p>
An estimated 1.5 million minimum wage workers will get a pay raise, which, in the words of Herman Mack, who works for $6 an hour in a Cleveland community center, "will allow you to put a little more food on the table, sustain yourself a little bit better than before."<p>
<img src="../../OtherImages/MinimumWageChart.gif" width="480" height="174" border="0"><p>
At the federal level, Democrats campaigned this fall on the promise that if they won a majority in Congress, they would quickly pass legislation to raise the federal minimum wage--which hasn't increased since 1997, the longest length of time at the same level since it was established-- to $7.25 an hour.<p>
The overwhelming support for the minimum wage referendums--which passed with 76 percent of the vote in Missouri and 66 percent in Arizona, for example--shows how important the issue is to a majority of Americans.<p>
But if Democrats have woken up to this fact, it is only because the current minimum wage is so low, and raising it costs them--and their corporate backers--very little. The proposed increase "is not a solution to poverty," says Matt Fellowes of the Brookings Institute. "This is, for the most part, a symbolic effort."<p>
While the minimum wage stayed at $5.15, since 1997, the cost of living increased by 26 percent overall, according to an article published by the Center on Budget and Policy Priorities and the Economic Policy Institute. A gallon of gas costs more than twice as much as it did nine years ago; the cost of medical care is up by 43 percent.<p>
Today, a full-time minimum wage worker earns $10,712 a year before taxes. That's well below the already low poverty line of $16,600 for a family of three. Even in the state with the highest minimum wage, Washington, full-time workers only get $16,515.<p>
When adjusted for inflation, the minimum wage is at the lowest level since 1955, and relative to the average wage, the minimum is at an all-time low for the post-Second World War period. Had the minimum wage kept pace with inflation since its high point in 1968, it would be $9.16 an hour.<p>
What these numbers mean in human terms is that workers have seen their living standards erode--and more and more families simply can't make ends meet.<p>
Two months after starting a minimum-wage job at a Target store in Missoula, Mont., Tara Dennis, a 23-year-old single mother had to quit--because she was <i>losing </i>money working there. After starting her job at Target, she lost her food stamps, and the rent at her subsidized housing apartment went up. Then she had to pay for day care costs while she was at work.<p>
"As a single mom," she told the Associated Press, "minimum wage isn't going to get me ahead. It's not even going to get me caught up."<p>

