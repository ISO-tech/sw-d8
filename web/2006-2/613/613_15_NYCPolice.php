<font face="Times New Roman, Times, serif" size="4">Anger still growing at racist police murder of Sean Bell</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Protesting NYC's killer cops</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Jared Rodriguez and Danny Katch</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 15, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--Two weeks after the police murder of Sean Bell on the morning of his wedding day, New York City is still boiling and protests are growing.<p>
There have been dozens of actions around the city to demand justice for Bell, a father of two, and his wounded friends Trent Benefield and Joseph Guzman, after police fired 50 rounds at the three unarmed men in their car outside a club in Jamaica, Queens.<p>
Two thousand people rallied outside the courthouse on December 6 at a demonstration called by the December 12 Movement, a Black nationalist group. Many in the crowd held signs charging the NYPD with murder and chanted, "No justice, no peace, NYPD off our streets!"<p>
After the rally, the crowd confronted police who were photographing and videotaping individuals. Protesters began to chant the number of shots fired by the NYPD when Bell was murdered.<p>
"One, two, three..." Upon reaching 50, the chants of "NYPD, you can't hide, we charge you with homicide" welled up in the crowd. Members of the Crips and Bloods gangs, placing their differences aside, marched together.<p>
Meanwhile, Mayor Michael Bloomberg has made a point of reaching out to the city's Black political leaders, a sharp contrast to the polarizing tactics of his predecessor, Rudolph Giuliani. In the wake of the shooting, Bloomberg immediately convened a meeting of Black religious leaders at City Hall and said that excessive force was used.<p>
"He's trying to learn from Giuliani's mistakes," explained antiwar activist Kim Rosario at the protest. "He is trying to engage Black leaders to pacify the Black community, in order to keep us from coming for justice."<p>
By contrast, the police murder of Amadou Diallo in 1999 triggered a series of protests and civil disobedience actions led by the Rev. Al Sharpton against police and the Giuliani administration.<p>
In this case, while Sharpton has been working with the Bell family, he didn't attend the December 6 demonstration--and he's repeatedly stressed to the media that our movement is not "against the police...we're anti-police brutality."<p>
But while Bloomberg has said some nice words, the actions of his police department show that nothing has really changed. The NYPD has gone on a manhunt on the south side of Queens, kicking down doors and rounding up young Black men in search of someone who will corroborate the story that there was a fourth man with a gun with Bell, Benefield and Guzman.<p>
This fairy tale should be put to rest with the release of the police department's own preliminary report, which makes no mention of a fourth man.<p>
This response underscores the reality that police brutality comes not from a "few bad apples," but is protected at the highest levels of the department. More than 100 people have been killed by the NYPD since the murder of Amadou Diallo, and tens of thousands have been wrongfully beaten and harassed.<p>
Some of these New Yorkers shared their stories at a forum organized in Harlem by the International Socialist Organization on December 8. One of the speakers was Margarita Rosario, who founded Parents Against Police Brutality after her son Anthony was killed in 1995.<p>
"I lost my son and my nephew, and the police thought I would lay down and die," Rosario told the meeting. "If I lay down, how may people will fall under the hands of the police?"<p>
This growing spirit of resistance can be felt in Jamaica, where there have been a series of protests organized by groups ranging from the NAACP to the New Black Panther Party that have spontaneously grown into the hundreds.<p>
Feeling this grassroots pressure, Sharpton has called for a demonstration on Saturday, December 16, at noon, which a number of unions and political groups have endorsed. The theme of the protest is "No business as usual," and it will take place at 59th Street and 5th Avenue in the heart of New York's holiday shopping district.<p>
Activists should work to make this the largest demonstration yet, make connections with each other and raise some concrete demands, which the movement has so far lacked, starting with the immediate arrest of the officers who fired on Sean Bell and the firing of NYPD commissioner Ray Kelly.<p>

