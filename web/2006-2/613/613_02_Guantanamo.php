<font face="Times New Roman, Times, serif" size="4">Feds want to restrict prisoners' access to their lawyers</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Left to rot inside Guant&aacute;namo</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 15, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">DETAINEES AT the U.S. prison camp in Guant&aacute;namo Bay, Cuba, may see their few remaining rights restricted even further if the Bush administration gets its way. According to a recent press report, the administration has proposed to limit the number of times detainees would be allowed to meet with their attorneys.<p>
The proposal was made about a single prisoner this summer in a government filing in a federal appeals court, but the White House wants the new regulations to apply to all of the prisoners at Guant&aacute;namo.<p>
If approved, the 430 detainees at the prison camp--many of whom have been kept there for five years, with no trial in sight--would be allowed just four meetings <i>in total</i> with their defense lawyers.<p>
The administration claims that defense lawyers' visits need to be limited because news of world events and other communications to detainees might incite them to violence.<p>
According to Navy Cmdr. Patrick McCarthy, who filed the brief, security at Guant&aacute;namo has been "compromised" by defense lawyers who told detainees about terrorist attacks in London, Iraq and Israel; a book on Abu Ghraib; and a speech given at an Amnesty International conference about the "war on terror." "Such materials could incite detainees to violence, leading to a destabilization of the camp," wrote McCarthy.<p>
But it's hard to imagine how anything could "incite detainees to violence" more than the inhumane conditions--including abusive interrogations, force feedings of hunger strikers and indefinite detentions--that they are subjected to.<p>
According to Inter Press Service (IPS), one lawyer at the Center for Constitutional Rights, which represents many of the detainees, said that he suspects the proposal is really aimed at controlling information coming <i>out </i>of Guant&aacute;namo from defense lawyers who are able to report on abuses their clients face at the hands of their U.S. captors.<p>
The restriction on meetings with lawyers comes on the heels of Bush's recent signing into law of the Military Commissions Act of 2006, which not only legalizes abusive interrogation techniques, but strips detainees of the 800-year-old right of habeus corpus--the right to challenge their detentions in court.<p>
These latest moves by the Bush administration undoubtedly consign innocent prisoners to more years in U.S. captivity.<p>
That includes a group of Uigher men--Muslims from a far Western region of China--who were picked up in Pakistan following the U.S. war on Afghanistan in 2001, and handed to the U.S. by Pakistani forces for ransoms of $5,000 each.<p>
In 2003, one interrogator told the Uighers, "You are innocent. I am closing the file on you." But as many as seven Uighers remain at Guant&aacute;namo to this day--because the U.S. claims it cannot find a country willing to accept them, and says they would face severe repression in China. "They were just in the wrong place at the wrong time and should be immediately released," their attorney, Susan Baker Manning, told the Associated Press.<p>
But according to Aziz Huq, an author and associate counsel to the Liberty and National Security Program at the Brennan Center for Justice, a recent court filing by Uigher detainees shows that their designation as "terrorists" may have come as a result of U.S. attempts to get China on board with the "war on terror."<p>
"In August 2002, as Iraq war drums was neared their zenith, Deputy Secretary of State Richard Armitage traveled to China to talk about the war that the United States had already decided to launch in Iraq," Huq recently wrote on the <i>Huffington Post </i>Web site. "But for the Chinese, the Uighers were an issue. They demanded that the United States recognize the Uighers as a terrorists, and designate Uigher political dissidents as members of a terrorist group. Armitage complied."<p>
With U.S. approval, Chinese authorities were reportedly even allowed to come to Guant&aacute;namo and interrogate the Uighers themselves--using "coercive techniques such as environmental manipulation, stress positions and stress deprivations."<p>
Now, under the Bush administration's proposal, unjustly imprisoned detainees like the Uighers will have even less opportunity to challenge their imprisonment.<p>

