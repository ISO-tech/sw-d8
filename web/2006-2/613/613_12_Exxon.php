<font face="Times New Roman, Times, serif" size="5"><b>When ExxonMobil teaches science</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 15, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">AFTER HEARING about the hole in the ozone layer, deforestation, fisheries being over-fished and the disappearance of many endangered species, it is depressing to think about the future of our planet. At this point, many aren't even surprised by the fact that, due to human activity, the earth is now facing the sixth mass extinction in history. But how could it have gotten this bad?<p>
Millions of people around the world are aware of the phenomenon that is the movie <i>An Inconvenient Truth</i>, in which former Vice President Al Gore discusses the science behind global climate change and its consequences on biodiversity.<p>
The movie is seen as a milestone for environmentalism: It has served to popularize and legitimize the findings of groups such as the National Academy of Science, whose researchers claim that "temperatures are, in fact, rising."<p>
But according to Laurie David, a producer of <i>Inconvenient Truth</i>, this is meaningless to the National Science Teachers Association (NSTA).<p>
After receiving word that many people thought the movie was an asset to any science classroom, the company offered 50,000 free copies of the movie to the NSTA for teachers to use. The offer was turned down. Why? David wrote in a <i>Washington Post</i> article that the NSTA is sponsored by none other than ExxonMobil, Shell and the American Petroleum Institute.<p>
These are the same people who funded pseudo-science to "disprove" global warming and spent massive amounts on media campaigns to mislead people into believing that scientists mostly disagree about whether climate change is reality.<p>
Not surprisingly, these are the same people waging a war of aggression to steal fossil fuels from another country so that they can be sold on the market and burned for fuel in industry, creating more carbon dioxide and accelerating the rise in temperatures even faster. And as always, a massive war budget means that education, among other social services, is in the toilet.<p>
So not only are many schools financially unequipped to teach science, but they are being further deprived of real and important science (for free) by oil profiteers.<p>
It also just so happens that the NSTA is the organization responsible for writing all of the national content standards that science teachers are required to teach--thus making their conferences none other than propaganda-fests for gas giants to distribute their agendas on what teenagers should learn.<p>
While Gore's political history is problematic, the science in his movie is essential to understanding the massive ecological disaster for which this profit-driven economy is responsible. We need today's and tomorrow's generations to be both activists and scientists so that we can dig ourselves out of the toxic hole that ExxonMobil will bury us in if it goes unchallenged, politically and scientifically.<br>
<b>Cynthia Little</b>, Burlington, Vt.<p>

