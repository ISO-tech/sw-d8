<font face="Arial, Helvetica, sans-serif" size="2" color="#990000">December 15, 2006 | Issue 613</font><p>

<font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>NO TO WAR AND OCCUPATION</b></font> <p>

<font face="Times New Roman, Times, serif" size="3"><b>"The U.S. occupation is the major source of violence and chaos in Iraq"</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="613_04_Arnove.shtml">The case for immediate withdrawal</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">The Iraq Study Group report blames the Iraqi victims for the disaster that exists today. But as Anthony Arnove explains, the main source of violence in Iraq is the U.S. occupation.<p>

<font face="Times New Roman, Times, serif" size="3"><b>The civil war in Iraq:</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="613_06_CivilWar.shtml">Bitter fruit of occupation</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">Imperial powers have always turned to the "divide-and-conquer" strategy to maintain their rule over occupied populations. The U.S. has drawn on this vile tradition in Iraq.<p>

<font face="Times New Roman, Times, serif" size="3"><b>Bush's new defense secretary:</b></font><br>
<font face="Times New Roman, Times, serif" size="4"><b><a href="613_11_RobertGates.shtml">Veteran of U.S. war crimes past</a></b></font><br>
<font face="Times New Roman, Times, serif" size="3">Robert Gates got the kid gloves' treatment before the Senate, but no one should think this veteran liar and right-wing warrior has become a compassionate peacenik.<p>

<font face="Arial, Helvetica, sans-serif" size="3" color="#990000"><b>WHAT WE THINK</b></font><p>

<font face="Times New Roman, Times, serif" size="3"><b>From mass immigrant rights marches to the vote against war</b></font><br>
