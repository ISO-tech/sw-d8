<font face="Times New Roman, Times, serif" size="5"><b>Protests shake U.S. allies in Lebanon</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 15, 2006 | Page 16</font><p>

<font face="Times New Roman, Times, serif" size="3">HUGE DEMONSTRATIONS in Lebanon's capital are demanding the resignation of Prime Minister Fouad Siniora and his pro-U.S. government.<p>
On December 10, hundreds of thousands of protesters answered a call to demonstrate by the Shiite party Hezbollah and its allies, creating a carnival-like atmosphere in the center of Beirut. That followed a massive march December 1, after which protesters erected hundreds of tents, creating a round-the-clock presence in the city center.<p>
In November, Siniora rejected Hezbollah's proposal for a national unity government that would give Hezbollah and its allies veto power in the Cabinet. In response, six pro-Hezbollah ministers resigned, and Hezbollah called for street protests.<p>
On November 21, Pierre Gemayel, Lebanon's industry minister and a leader of the right-wing Christian Phalange Party, was assassinated. U.S. officials all but accused the Syrian government, which is allied with Hezbollah, of carrying out the killing. But the U.S. attempt to exploit the assassination and put Hezbollah on the defensive has clearly failed.<p>
The U.S. continues to accuse Hezbollah of threatening a coup with its calls to demonstrate this month. But a year ago, the U.S. celebrated a wave of protests that toppled the prior government and brought Siniora to power--dubbing the movement the "Cedar revolution" and heralding it as a sign of democracy taking root in the Middle East.<p>
Coming in the wake of the assassination of the former Lebanese Prime Minister Rafik Hariri, the protests forced Syria to withdraw its forces from Lebanon and isolated Hezbollah, whose base of support is among the Shia poor in southern Lebanon.<p>
But Hezbollah's successful resistance to Israel's brutal assault on Lebanon this summer, which killed more than 1,000 civilians and drove a quarter of Lebanon's population from their homes, gave Hezbollah new credibility, gaining it support even among Lebanon's Christian, Druze and Sunni Muslim communities.<p>
Israel's bombardment, which the U.S. aided by expediting weapons transfers to the Israeli military, also damaged the Siniora government. At the December 10 demonstration, a huge banner covering the side of a building showed the prime minister hugging Secretary of State Condoleezza Rice. "Thanks Condy," read the banner, which also had an image of dead children killed by Israel during the summer bombardment.<p>
Since Israel's retreat, Hezbollah has been seeking to turn its enhanced popularity into greater political representation.<p>
When Gemayel was killed, George Bush himself denounced "attempts by Syria, Iran and their allies within Lebanon to foment instability and violence." But it now seems that most Lebanese blame Bush, not Hezbollah, for the "instability and violence."<p>
Plus, Hezbollah leaders have cultivated alliances with other political groups, making it more difficult for the U.S. to pit different factions against one another. Prominent Sunni cleric Fathi Yakan led prayers for the protesters in Beirut, and Hezbollah member of parliament Mohammad Raad met with Christian Maronite patriarch Nasrallah Boutros Sfeir to discuss how to end the crisis.<p>
Michel Aoun, a former general and leader of the Christian Free Patriotic Movement, also declared his support for the protests and called for new elections. "We are today at the last phase of our struggle before we consolidate our independence, freedom and sovereignty, because the government has proven to be a failure at all levels," said Aoun in a live video broadcast to the demonstrators in Beirut.<p>
The rising tide of protest represents a grave crisis for the government--and for U.S. plans to dominate Lebanon and the Middle East.<p>

