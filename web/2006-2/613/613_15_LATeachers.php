<font face="Times New Roman, Times, serif" size="5"><b>10,000 LA teachers rally to demand good contract</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs, United Teachers Los Angeles</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 15, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES--An estimated 10,000 members of United Teachers Los Angeles (UTLA) mobilized on December 6 to demand a 9 percent raise, smaller class sizes and more local control of our schools.<p>
More than 5,000 rallied outside of the downtown office of the Los Angeles Unified School District (LAUSD), while nearly as many attended a simultaneous mobilization in the city's San Fernando Valley region, according to a union press release.<p>
UTLA hasn't mobilized the membership on this scale since the 2000 contract campaign in which teachers won a 12 percent pay increase, but accepted concessions at the workplace. Since then, UTLA's salary agreements have consistently lagged behind inflation, and LAUSD has succeeded in forcing LA's already overcrowded classrooms to accept an average of two more students each.<p>
The rally tapped into teachers' growing anger at these conditions. Viviana Perez Sanchez came with 30 of her coworkers from Russell Elementary School in South Central LA. To the union's chant of, "We are the union, the mighty mighty union, fighting for justice, and for education," they added, "And for a raise, 10 percent!"<p>
"We deserve at least that much," Perez Sanchez explained. "Every time we settle for 2 or 3 percent, and it doesn't even keep up with the cost of living. Hopefully this rally will send a message, but we don't care if we have to go out on strike. Even though some of us can't afford it right now, we have to do whatever it takes."<p>
UTLA President A.J. Duffy attacked the massive LAUSD bureaucracy in his speech at the rally. "How can the district give a new superintendent [retired Navy Admiral David L. Brewer III] with no teaching experience $300,000 and a 20 percent raise?" said Duffy. "We demand a decent raise and an end to top-down mismanagement. We demand an end to one-size-fits-all mandated programs that rob our students of critical thinking skills and rob the joy from teaching."<p>
Elsewhere, dozens of teachers were heard chanting "9 percent or strike!" One teacher with a bullhorn led chants with, "When I say nine [percent], you say no! When I say 15, you say yes!"<p>
Duffy didn't mention a strike at the rally, and many UTLA leaders promoted the December 6 mobilizations as "the best way to avoid a strike."<p>
This conservative tack plays right into the hands of the district. Many teachers are indeed reluctant to go on strike, but the December 6 rallies show that a significant layer of teachers are ready to do whatever it takes to win our just demands.<p>
UTLA needs to promote the possibility of a springtime strike as the most powerful weapon that teachers have. We won't win a decent raise or real class-size reduction until we make the district afraid of a strike.<p>
<i>Gillian Russom contributed to this report</i>.<p>

