<p><a name="Troops"></a><font face="Times New Roman, Times, serif" size="4"><b>Bring the troops home now</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Some 65 activists from more than 20 organizations met and agreed to initiate a January 27 rally and march at Powell and Market Streets in solidarity with the protest planned for Washington, D.C., on the same date.<p>
Bob Watada, on behalf of his son Lt. Ehren Watada, who is the first officer to refuse deployment to Iraq, addressed the meeting and urged everyone to "support his son by organizing against this illegal war."<p>
Everyone agreed that the march's demands should begin with "Bring the troops home now from Iraq and Afghanistan" as well as "Stop racist attacks on Muslims and Arabs." There was some discussion about whether or not to include "End the occupation of Palestine" with the other two.<p>
Some felt that would alienate potential supporters, but after a debate, an overwhelming majority voted in favor of including Palestine as a central focus in the march and felt that it was especially important to highlight Israel's role as America's central military ally in attacking Lebanon and threatening Syria and Iran.<p>
<i>To get more information or help organize the march, contact the January 27 Antiwar Committee at 510-484-5242 or e-mail <a href="mailto:snehal100@hotmail.com" target="_blank">snehal100@hotmail.com</a></i>.<p>
-- In <b>New Britain, Conn., </b>close to 80 people turned out for Connecticut United For Peace's (CTUP) December 9 "Out now!" antiwar conference.<p>
The conference provided an opportunity for activists to deepen their understanding of the war in Iraq and the politics of the broader Middle East and to strategize ways to include larger numbers of people in the upcoming January 27 national demonstration in Washington, D.C.<p>
The strategizing session took up tactical questions of rebuilding mass demonstrations as well as the need to reach out to antiwar GIs, Arabs and Muslims in building for the January 27 march.<p>
<i>Todd Chretien and Jay Poppa contributed to this report</i>.<p>

