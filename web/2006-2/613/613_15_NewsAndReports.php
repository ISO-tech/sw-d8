<p><a name="Equal"></a><font face="Times New Roman, Times, serif" size="4"><b>Gay marriage is a right</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Tristan Brosnan</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SPRINGFIELD, Mass.--After state legislators refused to vote on a gay marriage ban November 9, local antigay Catholic groups called for a demonstration here. These demonstrations were organized by Catholic Citizen and Vote On Marriage, two gay-bashing groups that veil their bigotry with the claim that legislators and voters, not the courts, should decide whether gay couples get the right to marry.<p>
As gay rights protester Tony Shannon put it, "Instead of saying we're against gay marriage, they put a spin on the debate by saying it's about democracy, that people should be able to vote on rights of gays and lesbians, and I'm just sick of it."<p>
Marching in loud and angry, progay rights counterprotesters overtook the steps of City Hall, where the bigots were demonstrating. In the face of chants of "Bigots, go home," the antigay bigots fled the steps for the park across the street.<p>
The counterdemonstration had an amazing feeling of confidence and victory. In solidarity, minister Luis Alvarenga, a local leader in the immigrant rights movement, spoke of the need for all oppressed groups to unite and fight against all forms of discrimination and oppression.<p>

