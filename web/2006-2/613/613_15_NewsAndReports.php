<font face="Times New Roman, Times, serif" size="4"><b>No to "no match," yes to immigrants</b></font><p>

<font face="Times New Roman, Times, serif" size="3">BERKELEY, Calif.--The Berkeley City Council passed a resolution October 24 formally opposing the Homeland Security Department's proposed changes to Social Security "no-match" regulations.<p>
This was a joint effort on the part of immigrant rights activists from area unions, churches and the community. San Francisco has also passed a similar resolution, and activists are working on getting Oakland to do the same.<p>
The new rules would give power to the employer to act as immigration cop and fire workers whose Social Security numbers at work don't match the number that the Social Security Administration has on file.<p>
Employers are already using this as cover to fire workers who are leading organizing drives in their workplaces. Local examples include the Woodfin Hotel in nearby Emeryville, where workers are fighting to force management to obey a living wage ordinance, and Cintas, where workers in San Jose are fighting to form a union.<p>
City officials cannot legally force private employers to resist the new federal regulations, but by taking a stand, they can support activists fighting for justice in their workplaces.<p>

-- In <b>Chicago, </b>immigrant rights supporters are challenging Jays Foods' plan to fire workers on December 16 who received no-match letters and Stockyard Meats' plan to fire workers December 26. Workers at Jays are debating a possible boycott of the company and the most effective action to stop the terminations.<p>
Activists from the Chicago Workers' Collaborative and the March 10 Movement attended the recently held Illinois Legislative Latino Caucus Foundation (ILLCF) conference to appeal for support for Jays workers.<p>
After discussion, conference organizers agreed to return food from Jays included in their lunch. And the ILLCF voted to demand that Jays end plans to fire immigrant workers and to support a boycott of the company.<p>

-- In <b>Madison, Wis., </b>organizers from UNITE HERE, Chicago Workers' Collaborative, Interfaith Coalition for Worker Justice, Latinos United for Change and Advancement, ISO, Student Labor Action Coalition and fired Cintas workers met to discuss their response to the termination of 15 workers from the local Cintas plant as part of the 400 no-match terminations by the company nationwide.<p>
Activists have created a rapid response network to mobilize against terminations and raids. They discussed organizing a rally in early spring and proposing legislation protecting workers from no-match letters.<p>
<i>Shaun Harkin, Kathryn Lybarger and Alex Stone contributed to this report</i>.<p>

