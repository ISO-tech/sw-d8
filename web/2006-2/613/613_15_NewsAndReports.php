<p><a name="Mumia"></a><font face="Times New Roman, Times, serif" size="4"><b>Free Mumia</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Jaan Fulano and Eric Augenbraun</b></font><p>

<font face="Times New Roman, Times, serif" size="3">PHILADELPHIA--Close to 500 people gathered at City Hall on December 9 in protest of Mumia Abu-Jamal's 25-year incarceration. The protest, called by the International Concerned Friends and Family of Mumia Abu-Jamal, drew contingents from around the country and featured spirited speeches.<p>
Abu-Jamal, a former Black Panther and radical journalist, was accused of killing a white police officer in 1981. His supporters claim that his trial was unfair and was the product of racial and political persecution. Veteran activist Pam Africa spoke of flawed police testimony and intimidated witnesses, and protesters carried signs connecting the criminal justice system and racism.<p>
Speakers charged Black Democratic politicians with doing little to help Abu-Jamal's case. In particular, speakers singled out Rep. Chaka Fattah (D-Pa.), who supported a House resolution denouncing Saint-Denis, a French town that recently named a street after Mumia.<p>
The event was considerably smaller than in years past, but organizers and speakers stressed the important role of protest, even small ones.<p>

