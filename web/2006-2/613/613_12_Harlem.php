<font face="Arial, Helvetica, sans-serif" size="2"><b>VIEWS AND VOICES</b></font><br>
<font face="Times New Roman, Times, serif" size="4">Police tower moves in along with the gentrifiers</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Watching over Harlem</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 15, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">A SKY Watch surveillance tower has been placed at the intersection of 129th Street and Malcolm X Boulevard in Harlem by the New York Police Department (NYPD).<p>
This mobile surveillance unit, which gives a cop a line of sight from 20 feet up, is equipped with four cameras, a high-powered spotlight and various sensors. The digital cameras continue recording when the booth is unstaffed and save the video to a computer hard drive. The tower resembles those used as guard watchtowers in prisons.<p>
Sky Watch towers cost from $40,000 to $100,000 apiece. The NYPD is currently leasing one or two--one was erected at the intersection of Carroll Street and Troy Avenue in the Crown Heights section of Brooklyn. The NYPD hopes to eventually have five of the towers in place around the city.<p>
Sky Watch is a product of New Heights Manufacturing in Ellaville, Ga. It is currently being used by the U.S. military in Iraq and the U.S. Border Patrol. In a 2004 article in <i>Quickstart</i> (the newsletter of the Georgia Department of Technical and Adult Education), Sky Watch was said to be a powerful crime deterrent, due to its bulletproof windows, infrared cameras, high-powered spotlights and M-16 ready gun ports.<p>
The prototype for Sky Watch was called "Huntmaster" and was developed as a hunting blind. The manufacturer soon realized that developing this technology for the security industry would propel them into the world market. They began selling the product to the U.S. government in 1998.<p>
The article in <i>Quickstart</i> ends by praising New Heights Manufacturing for "developing additional security and surveillance devices that serve as 'force multipliers,' tools that allow a small number of personnel to pack a bigger punch, defending America on the front lines of the war against terror."<p>
It is interesting to think that such a device would be necessary in Harlem, when all statistics say that the crime rate in the area and the city as a whole has gone down 25 percent in the last five years. The NYPD says the tower was placed in Harlem to combat a surge in murders. It was difficult to find any documentation of a rise in murder rates in Harlem to correspond with the police department's justification for placing Sky Watch in the neighborhood.<p>
The only stories I could find about crime rates in Harlem were about how they have fallen, making it a "hot" new time to invest in real estate development.<p>
The Sky Watch tower was installed on the same intersection as two new luxury condo complexes, the Lenox and the Lenox Grand. Many Harlem residents feel that this is the real reason for the use of the Sky Watch tower. Lloyd Estridge an electrician who worked on the Lenox told the <i>New York Post</i>, "They wait until multimillion-dollar apartments go up, and then they want to watch over Harlem."<p>
I went to 129 Street and Lenox Avenue to check out the tower for myself, but it was no longer there. I decided to go into a local copy center and ask if anyone had noticed the tower and if anyone knew where the Sky Watch tower had been relocated. A couple of residents responded immediately, "I saw that thing, I don't know why they put it in this area."<p>
One man said that the police had been moving it all over the neighborhood. He asked police officers why they would place the tower in his neighborhood, but the officers refused to give him a reason. He said, "I take issue with that thing showing up on my block." <br>
<b>Dawning Greenstreet</b>, New York City<p>

