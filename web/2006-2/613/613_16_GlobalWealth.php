<font face="Times New Roman, Times, serif" size="5"><b>The growing global gap between rich and poor</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Eric Ruder</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 15, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">THE RICHEST 1 percent of the world's population owns 40 percent of all wealth, and the richest 10 percent of adults owns 85 percent of global wealth, according to a newly released study. At the other end of the spectrum, the bottom half of the population owns less than 1 percent of total wealth.<p>
The study, conducted in 2000 by the World Institute for Development Economics Research of the United Nations University, is the most comprehensive of its kind. The U.S. and Japan have produced the largest share of the extremely wealthy, accounting together for almost two-thirds of those in the top 1 percent.<p>
"These levels of inequality are grotesque," said Duncan Green, research director at Oxfam. "It is impossible to justify such vast wealth when 800 million people go to bed hungry every night. The good news is that redistribution would only have to be relatively small. Such are the vast assets of the rich that giving up a small part of their wealth could transform the lives of millions."<p>
Wealth inequality is also extreme within countries, especially the United States. In the U.S., the richest 10 percent controls 70 percent of the country's wealth, making the U.S. one of the most unequal among advanced countries.<p>
Increasing levels of debt, especially mortgages, mean that many workers in countries have no wealth, even if they own their own homes. "Many people in high-income countries have negative net worth and--somewhat paradoxically--are among the poorest people in the world in terms of household wealth," say the study's authors.<p>

