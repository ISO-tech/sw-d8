<font face="Times New Roman, Times, serif" size="5"><b>U.S. leads world in locking people up</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 15, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">ONE OUT of every 32 U.S. adults was either in jail, on probation or on parole at the end of last year, according to the Justice Department. The new federal statistics show that the U.S. continues to incarcerate its citizens at a rate higher than any other industrialized nation.<p>
A record 2.2 million Americans were in jail--a 2.7 percent increase over the previous year, and a 35 percent rise since 1995. When those on probation and parole are added in, the number tops 7 million, or 3 percent of the adult population of the U.S.<p>
A report disclosing the statistics doesn't give any reason for the increase in the prison population, but experts say there has not been an increase in violent crime--on the contrary, violent crime rates have declined recently. Instead, they say, harsh federal and state sentencing laws, especially those mandating sentences for nonviolent drug offenses, are continuing to fill up prisons.<p>
Prisoners sentenced for drug crimes continue to be the largest group of federal inmates, making up 55 percent of the prison population in 2003, according to the Justice Department. More than one-quarter of the 4.1 million men and women on probation last year were under supervision for a drug law violation.<p>
Immigration offenses are also snaring more people into the criminal justice system. Between 1995 and 2003, convictions for immigration offenses rose by 394 percent--and prisoners charged with immigration violations now make up 10 percent of federal and state inmates.<p>
The racism of the justice system is as apparent as ever. Among those ages 25 to 29, 8.1 percent of Black men--about one in 13--are incarcerated, compared with 2.6 percent of Latino men and 1.1 percent of white men.<p>
Overall, in 2005, Blacks made up 40 percent of all inmates--three times larger than their proportion in the U.S. population. According to the Sentencing Project, in seven states--Iowa, Vermont, New Jersey, Connecticut, Wisconsin, North Dakota and South Dakota--African Americans are incarcerated at more than 10 times the rate of whites.<p>
The new federal statistics also show a rise in the female prison population--which increased by 2.6 percent last year to make up a total of 7 percent of the inmate population, more than 200,000 people.<p>
"These numbers are not worthy of celebration," James Alan Fox, a criminal justice professor at Northeastern University in Boston, told the <i>Los Angeles Times</i>. "We are becoming more punitive. We put so many more people into prison--the question is what happens to them when they get out."<p>

