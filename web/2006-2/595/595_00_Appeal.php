<font face="Times New Roman, Times, serif" size="4">Appeal from the left in Lebanon:</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Call for action against Israel's war on the Palestine and Lebanon</b></font><p>

<font face="Times New Roman, Times, serif" size="3">The Israeli war machine continues with its war for the ethnic cleansing of Palestine and Lebanon. Attacks against civilian targets are continuing, resulting in scores of dead and wounded in Gaza, the West Bank and Lebanon, with the vocal and tacit support of the international community. For the past three days, attacks on Lebanon resulted in the destruction of major civilian roads, cutting off whole areas and hindering relief efforts.<p>
Whole families are being trapped under rubble in South Lebanon. The southern suburbs of Beirut, one of the most densely populated and poorest neighborhoods in the country, are currently witnessing intensive bombardment with the majority of civilians trapped due to the bombing of bridges and roads. Just as in Gaza, Israel is attempting to ethnically cleanse a population under the pretext of replying to the legitimate acts of the resistance.<p>
The issue for us is clear. It is the presence of a fascist and racist regime in Israel. U.S. imperialism, its allies and military power in Israel prove that the issue has nothing to do with democracy or freedom. Their democracy is nothing but murder, destruction of houses, bombardment of electrical and fuel installations, and the indiscriminate targeting of children. The Israeli regime has indicated that they will not stop. In these days, there is no space for compromise. It is either resistance or collaboration.<p>
The international community is fully responsible for the massacres against the people of Lebanon and Palestine. Our reply is a full-scale popular resistance against imperialism and its allies in the Arab regimes and the West. In the past several days, a group of organizations in Beirut initiated an open sit-in in the center of the city to mobilize for actions against Israeli aggression against Gaza and in support for the legitimate acts of the resistance. With the targeting of Lebanon, the sit-in has turned into a meeting point for mobilization of relief and solidarity actions.<p>
We call on all our friends in the world to act in solidarity with the people of Palestine and Lebanon through demonstrations and rallies outside Israeli and U.S. embassies calling for an immediate and unconditional end of the occupation, the release of all prisoners of war in Israel, and the rights of people.<p>
No to Imperialism and Occupation!<br>
No to the Indifference of the "International Community"!<br>
No to Dictatorships!<p>
