<font face="Times New Roman, Times, serif" size="4">Palestinian doctor writing from Gaza</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Nobody is safe"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">July 14, 2006 | Page 5</font><p>

<font face="Times New Roman, Times, serif" size="3">MONA EL-FARRA is a physician and human rights activist living in Gaza. She has been writing <a href="http://www.fromgaza.blogspot.com" target="_blank">a blog</a> from her home since the beginning of Israel's assault--when the flow of electricity permits. Here, we reprint excerpts from her blog entries.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="4"><b>Saturday, July 1</font></b><p>
<font face="Times New Roman, Times, serif" size="3">MY FRIEND Hoda lives next to the Ministry of Interior building in Gaza that was hit last night at 2 a.m. with two rockets (please forgive me about the accuracy as l am starting to lose track of days and nights and how many times we've been attacked).<p>
Hoda told me that the whole building was shaking, and that she went out in her pajamas along with all the other residents out in their sleepwear. Children's faces were pale, and some were crying hysterically. Fumes filled the place, and the flat next door where a family with six children lives was severely damaged. There was a large fire, and the fire department used her flat to put it out.<p>
I live 150 meters from Hoda's place--nobody is safe, no one is immune.<p>
What happened with Hoda reminded me of the night when the late President Yasser Arafat's headquarters in Gaza were attacked two years ago. I live nearby--too close--and that night 37 shells hit that building.<p>
The power is still off. We had it for three hours yesterday--enough to recharge my laptop and mobile phone, and to do some cooking.<p>
I am deeply concerned about the hospitals. The fuel supply to run the generators is dwindling, and the borders are completely sealed since the attack. No fuel has been allowed into Gaza since then, and drugs and medical supplies are also running out.<p>
Because of the previous and ongoing sanctions, our stores of drugs are exhausted. The water supply is scarce too--we must ration use. We are experiencing a humanitarian disaster.<p>
When the jet fighters break the sound barrier, we experience sonic booms--a terrifying sort of raid. These happen at least seven different times a day--throughout the day and night.<p>
How can I explain the experience to you? If I am sleeping, my bed shakes tremendously, and my daughter jumps to my side, shivering with fear. Then, both of us end up on the floor, my heart beating very fast. I try to calm my daughter, and she tries to calm me.<p>
If I am awake, I flinch and scream loudly--I cannot help myself. Okay, I may be a doctor, a mature middle-aged woman with a lot experience, and an activist too, but this booming makes me hysterical. After all, we are all humans, and we each have our own threshold.<p>
As a result of the booms, hospitals take in a large number of psychologically traumatized children.<p>
With 1.5 million residents in Gaza facing collective punishment, feelings of hate will grow larger. These savage assaults on the population will not bring peace or security to Israel, only justice and peace will.<p>
Israel is talking about security of its citizens in the face of militiamen armed with some homemade rockets, Israel is talking about terrorism--but what are these sonic booms and attacks on power plants but state terrorism?<p>

<font face="Times New Roman, Times, serif" size="4"><b>Wednesday, July 5</b></font><p>
<font face="Times New Roman, Times, serif" size="3">A BIG explosion, and so loud, and I'm fully awake. So is my daughter Sondos. We hardly can see anything--it is very dark. A drone hit the Ministry of Interior building again with a missile, completely destroying it. <p>
I contact my friend Hoda to find her screaming, shrieking in pain, trapped under the windows of her flat, which were broken by the blast. Fumes fill the place, and she is waiting for the emergency team to evacuate her.<p>
I can hear the hysterical sounds of her neighbors over the phone. I feel helpless--I don't know what to do for my friend. I phone again to offer help, and she tells me that she is okay, but I know she's not.<p>
When I visited Hoda four hours ago, we both were tense, and a third friend asked us to talk about anything but politics or what is going on in the Palestinian scene. We tried to, but couldn't. I left her and walked home.<p>
The sound of airplanes is high in the sky, and I can't go to Hoda, because I cannot leave my daughter alone. I expect more air raids, but I'm sure some other friends are with her now (she lives alone).<p>
I hear another explosion, and I learn that they hit an empty school in another neighborhood.<p>


