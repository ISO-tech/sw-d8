<font face="Times New Roman, Times, serif" size="5"><b>Discontent simmers over UTLA deal with mayor</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Randy Childs, United Teachers Los Angeles</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 14, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">LOS ANGELES--On June 21, Los Angeles Mayor Antonio Villaraigosa announced that he had reached an "historic agreement for comprehensive reform of Los Angeles schools" with legislators and United Teachers Los Angeles (UTLA).<p>
This announcement came after a year in which Villaraigosa--first as mayoral candidate, then while in office--campaigned to gain full mayoral control of the Los Angeles Unified School District (LAUSD).<p>
Villaraigosa--a former UTLA staffer--rode the union's endorsement, and campaign contributions amounting to hundreds of thousands of dollars of our dues money, to victory in the election. <p>
Then, he invited Chicago's Mayor Richard Daley and New York City's Mayor Michael Bloomberg to his inauguration ceremony. Both Daley and Bloomberg have used mayoral control of the schools to viciously attack the teachers' unions in their respective cities.<p>
When Villaraigosa started using his bully pulpit as mayor to campaign hard for the same powers as Daley and Bloomberg, UTLA found itself in the position of opposing the centerpiece proposal of "our" endorsed candidate. <p>
In a contortion that would have impressed Houdini, UTLA rightly attacked Villaraigosa's takeover plan as "undemocratic" while simultaneously thanking the mayor for "raising the issue of education" and seeking a partnership with him to "fix what's wrong with LAUSD."<p>
Villaraigosa rescinded his initial promise to bring his mayoral control proposal to a popular referendum in LA when he realized that he would never win such a vote, retreating to the state legislature. But by June, after UTLA lobbied hard to break off some of the mayor's liberal supporters, it became clear that mayoral control lacked the votes it needed to get off the ground even in Sacramento.<p>
After playing a key role in defeating mayoral takeover for now, UTLA sadly seems to have made some unnecessary concessions in its deal with Villaraigosa that revive some of the most dangerous elements of the mayor's initial proposal. <p>
For example, the unelected LAUSD Superintendent will gain some unclear amount of power at the expense of the elected School Board, with the mayor having a hand in budget decisions and veto power over the selection of a Superintendent. <p>
Confusingly, UTLA leaders are glowing in their descriptions of the agreement. "We have always supported a partnership between the Mayor, the school district, teachers and parents," said UTLA President AJ Duffy, a longtime union dissident before winning office last year. <p>
UTLA Vice President Joshua Pechthalt, a founding member of Progressive Educators for Action (PEAC), added, "I think it's a very big deal for us," emphasizing the agreement's promised increase in educational autonomy for schools and teachers. PEAC is the rank-and-file group within UTLA that was key to the election of UTLA's new leadership last year.<p>
Antonio Villaraigosa is a rising star in California's Democratic Party machine and is eager to advance his career by proving his willingness to serve the corporate paymasters of his party. That's why he brought up his union-busting takeover scheme in the first place.<p>
UTLA needs to devote itself to grassroots organizing to rebuild our union at the school sites and forge alliances with our natural allies in the community. We will never win by cutting deals with politicians who make vague promises about "partnership" while saying nothing about the overcrowding, underfunding and segregation that plague our schools.<p>

