<font face="Times New Roman, Times, serif" size="5"><b>Why Israel is on the attack in Gaza</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Paul D'Amato</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 14, 2006 | Page 13</font><p>

<font face="Times New Roman, Times, serif" size="3">COVERAGE IN the U.S. media of the Israeli-Palestinian conflict is presented through a camera obscura--a lens that turns images upside down. <p>
The June kidnapping of an Israeli soldier is presented as a "terrorist attack" to which Israel must respond, as opposed to what it is--an act of resistance against military occupation. The kidnapped Israeli soldier was a tank gunner stationed on the edge of Gaza.<p>
The Israeli army surrounds Gaza like a noose, routinely closing its borders to traffic, thereby destroying its economic life. And since the pullout of Israeli settlers from Gaza, the Israeli army has continued to attack it on a regular basis.<p>
An Israeli artillery shell killed eight and injured 32 on a beach in Beit Lahiya on June 9; Israeli aircraft missiles killed 11 Palestinians on June 13 in Gaza City; and on June 20, at least one Israeli missile, apparently aimed at resistance leaders, killed three children and wounded 15.<p>
The current invasion that began on June 25 has left 52 dead and counting. "Israeli warplanes and tanks never stop, day or night, firing heavy artillery against every target possible," Rami Almeghari reports from Gaza. "Homes, institutions and infrastructure never escape the Israeli shelling; power and water plants have been severely hit so far, main roads have been damaged, buildings and homes have been shelled."<p>
Almeghari reports that Israel has completely sealed off Gaza from the outside world, and that the 1.4 million Gaza residents sit "huddled in the darkness, behind sandbags, watching the thousands of Israeli tanks lined up on the border, and fearing for the worst."<p>
According to the report of Prof. John Dugard, Special Rapporteur on the situation of human rights in the Occupied Palestinian Territories, Israel has Gaza "under siege," and "plans to retain ultimate control over Gaza by controlling its borders, territorial sea and airspace."<p>
Israel is literally imposing collective punishment on the entire population of Gaza, ostensibly because one Israeli soldier was kidnapped. This is clearly not the reason.<p>
The Israeli daily newspaper <i>Yediot Ahronot</i> reports that the current invasion of Gaza was planned before the kidnapping of the Israeli soldier. The Israeli occupying army is merely using it as a pretext.<p>
The conflict between Israel and the Palestinian pits a military behemoth (Israel) attempting to impose its desire for a "greater Israel" on a far weaker opponent, and the statistics show it.<p>
Israel has the world's 15th largest military budget, and the highest per capita military spending of any country including the U.S. By way of comparison, the Palestinian Authority's <i>total "state" budget</i> (we have to put "state" in brackets because Israel will not allow it to function as a normal state) is $2 billion, that is, less than a third of Israel's military budget alone. <p>
The formidable size and strength of the Israeli military is dependent on generous U.S. military and economic aid, which exceeds $5 billion per year.<p>
Israel uses Apache attack helicopters, Lockheed Martin F-16I Fighting Falcons and Boeing F-15Is, which fire U.S.-manufactured AMRAAM, Sidewinder and Sparrow missiles, against Palestinian targets. The Palestinians, on the other hand, have no air force, and their primitively improvised rockets are almost completely ineffectual against Israeli targets. <p>
Whereas the Palestinians now have exactly one Israeli prisoner, the Israeli's hold 9,600 Palestinians. Israel has detained more than 650,000 Palestinians since 1967.<p>
Israeli forces have demolished more than 4,000 Palestinian homes since September 2000; Palestinians have not demolished a single Israeli home. Whereas Israeli forces have killed more than 4,000 Palestinians since September 29, 2000, Palestinians have killed fewer than 1,100 Israelis.<p>
Since 2000, Israel has established more than 60 settlements on land confiscated from Palestinians, and has resettled more than 380,000 Israeli citizens in territory seized from Palestinians in the 1967 war. Under these circumstances, the demand of the Palestinian fighters who captured the Israeli soldier that Israel release some of the thousands of Palestinians it has kidnapped is perfectly reasonable.<p>

