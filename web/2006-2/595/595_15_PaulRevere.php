<font face="Times New Roman, Times, serif" size="5"><b>"Immigrants in, racists out!"</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">July 14, 2006 | Page 15</font><p>

<font face="Times New Roman, Times, serif" size="3">THE PAUL Revere Riders, a group allied with the anti-immigrant Minutemen, are traveling across the country to further their racist campaign to build a wall on the U.S.-Mexico border and oppose the flow of undocumented workers. But everywhere they turn up, they are met by large numbers of counter-protesters.<p>
-- In <b>Chicago, </b>more than 150 protesters turned out July 8 in front of the Mexican Consulate to confront the Riders. Members of the Illinois Minutemen joined the Riders, but the taunts of the 20 or so white supremacists were easily drowned out by the large and spirited crowd of anti-racists.<p>
A militant tone was set early in the demonstration, with activists encircling the racists and chanting, "Open up the borders, shut the racists down!" and "No Minutemen, no KKK, no fascist USA."<p>
Police then forced the two groups apart, cramming the small cluster of right-wingers on the corner, while anti-racist activists filled out the remainder of the block and drew support from passers-by with signs like, "Honk against hate."<p>
-- In <b>Los Angeles, </b>around 200 Minutemen were prepared to march down Hollywood Boulevard in the early evening hours of July 8. As they assembled on one corner of the street, they were quickly matched in numbers by a crowd of anti-Minutemen protesters. <p>
As Minutemen and their supporters made hate speeches from the bed of a blue pick-up truck, the crowd of protesters drowned them out with chants of "Racists go home!" and "Minutemen, KKK, racist scum, go away!"<p>
Calling in several squads of back-up cops in riot gear, the LAPD lined the street as a barrier to protect the racists from the protesters. At different points throughout the evening, the LAPD waded into the crowd, swinging batons and striking protesters.<p>
It was obvious that several cops took pleasure in the assaults, smiling broadly. Meanwhile, the Minutemen cheered, apparently pleased to see other racists in uniform using excessive force against the diverse crowd. <p>
With groups like the Minutemen being legitimized by politicians' legislative proposals and the mainstream media, it is important that we confront them wherever they rear their ugly heads. In Los Angeles, where more than 1 million marched for immigrant rights in March, the task of persuading larger numbers to confront the Minutemen is urgent.<p>
-- In <b>Madison, Wis.,</b> with whistles, drums, signs demanding amnesty and determination, a group of 80 local activists showed up on the steps of the Capitol building on July 7 to confront the Riders. The counter-protest was organized with only a day's notice after news of the Riders' arrival in Madison was circulated at the Midwest Social Forum in Milwaukee the day before. <p>
The success of this last-minute mobilization, which drove the racists from the streets ahead of schedule, proves the dedication of all involved in the immigrant rights movement in Madison. It also goes to show that we are willing and able to fight for what is just--the legalization and unity of all workers. <p>
-- In <b>New York City, </b>more than 80 people gathered in midtown Manhattan to protest a June 30 anti-immigrant rally organized by New Yorkers for Immigration Control and Enforcement (NYICE), which is affiliated with the Minutemen and has ties with other white supremacist groups.<p>
This NYICE rally--their third in a month--targeted the offices of Senators Clinton and Schumer to protest the guest-worker program backed by both politicians.<p>
Chasing bigots out of our communities is one of the tasks that the immigrant rights movement and its allies cannot postpone. Activists in New York City are now gearing up to counter the United Patriots of America, who have been intimidating day laborers at pick-up sites in New Jersey.<p>
<i>Josh Gryniewicz, Kurt Krueger, Emmanuel Santos and Lauren Schmidt contributed to this report</i>.<p>

