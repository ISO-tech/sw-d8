<font face="Times New Roman, Times, serif" size="5"><b>Workers pay the price in N.J. budget deal</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nagesh Rao, AFT Local 2364</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 14, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">A SIX-day shutdown of the New Jersey state government ended July 6 with a "compromise" that is a sign of worse things to come. <p>
At a press conference, Democratic Gov. Jon Corzine and Assembly Speaker John Roberts congratulated each other for cutting a deal to increase the regressive sales tax that ended their squabble over how best to squeeze workers in the new budget.<p>
Nearly 100,000 state employees lost workdays and pay during the shutdown. And the compromise budget is certain to result in further cuts to social services, workers' pay and benefits, and state jobs.<p>
Faced with a shortfall of $4.5 billion, Corzine's budget calls for more than $2.5 billion in spending reductions. Education--both K-12 and higher education--health care and municipal aid stand to lose $2 billion. <p>
Some 75 programs will be eliminated, 130 more will have funds slashed, and another 30 programs will see a "cut back in growth." The budget underfunds state employees' pensions by 30 percent and introduces new restrictions on pension eligibility. <p>
State colleges and universities will lose $169 million in funding. In addition, the state is going back on its obligation to refund schools for faculty pay raises and benefit increases mandated by the current union contract--amounting to an additional $121 million in cutbacks to higher education. <p>
Nearly 1,000 other state jobs have already been eliminated, and colleges are beginning to consider temporary shutdowns and furloughs to make up for the reductions.<p>
The budget does eliminate income taxes for some 400,000 of the state's poorest residents by raising the income tax threshold from $20,000 to $25,000. But this pales in comparison to the effect of an increased state sales tax and rollbacks in social services.<p>
While imposing a 2 percent surcharge on corporate taxes for three years, the budget at the same time eliminates "a larger set of anti-growth taxes on businesses," as Corzine put it in a speech earlier this year. The net result is an effective <i>cut </i>in corporate taxes to the tune of $282 million, according to the <i>Philadelphia Inquirer.<p>
</i>On top of this, Corzine proposed a 1 percent increase in the sales tax, which hits workers and the poor the hardest.<p>
Democratic lawmakers, led by Roberts, were joined by Republicans in opposing the increase. Instead, they wanted even deeper cuts to pension contributions and lower property taxes across the state. This dispute is what caused the budget standoff.<p>
Under the compromise that ended the six-day shutdown, half of the revenues from the 1 percent sales tax increase will go to lowering property taxes.<p>
Media reports highlighted Corzine's "rousing speech" announcing the end of the shutdown. <p>
In fact, his words were a thinly veiled threat aimed at state workers and unions. Corzine made it clear that the state's budget crisis is far from over, and that this round of concessions is only the beginning. He repeatedly announced that the state would create "more efficiencies" that would result in further "cost-savings" in the coming years, and pointedly singled out upcoming contract negotiations as the place where such "efficiencies" could be worked out.<p>
This is a clear warning to unions whose contracts expire next year--especially at the state's higher education institutions--to prepare for more cuts. <p>
Estimates suggest a minimum shortfall of $1.5 billion next year, meaning that contract negotiations will coincide with another round of calls for austerity and spending reductions. State workers and unions must start organizing now to prepare for a tough fight next year.<p>

