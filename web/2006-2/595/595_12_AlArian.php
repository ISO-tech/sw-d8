<font face="Times New Roman, Times, serif" size="5"><b>Sami Al-Arian in a house of horrors</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">July 14, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">I JUST read in another publication that Dr. Sami Al-Arian has been moved from a correctional facility in Tallahassee, Fla., to the federal prison in Atlanta, Ga. He received no food or water for 15 hours during the process. He is being held in solitary confinement and 23-hour-a-day lockdown, and has also been denied visitation rights for 30 days. <p>
I too was briefly sent to Atlanta before I was transferred here, so I thought I would share some firsthand accounts of the place. <p>
Firstly, the Atlanta prison is both a regular federal prison and a federal transfer facility. Dr. Al-Arian is at the transfer facility part of the complex, as was I. He is likely (in my view) to stay there until deportation. Deportees tend to be sent to transfer centers, in my experience.<p>
The Atlanta federal prison is a horrible facility--the worst I have been to. It's the oldest federal prison still in use. It was built in 1902, and Eugene V. Debs did his time there. Not being fed for hours during processing is normal. I wasn't fed for seven hours. No one is fed until after processing at any federal facility.<p>
Atlanta suffers from horrible overcrowding--four or five men in a cell meant for two is the norm. The first two men get a bed. The others sleep on the floor. <p>
Frankly, I think the fact that Dr. Al-Arian is in solitary is a good thing. Privacy is impossible otherwise, not to mention the stress of sharing cramped quarters with four other men nearly all day for weeks or months at a time is hell. At night, the people sleeping on the floor stuff their towels under the doors to keep rats from coming in to bite you.<p>
Because the water was on only sporadically, the toilet in the cell cannot be flushed. You only get to shower once a week for five minutes under cold water. After the five minutes are up, the guard opens the curtain and forces you out. <p>
The guards are cruel and uncaring. You could be being beaten in your cell and yell for help, and get no response--another reason why solitary for Dr. Al-Arian is best. <p>
In all honesty, I think Atlanta should be shut down immediately. The mere mention of Atlanta can bring fear to the most hardened con. I've seen large, tough men freak out when they find out they're going to Atlanta. <p>
The fact that Dr. Al-Arian is in solitary may mean that he will be shielded from the worst of it, but that poor man is in a house of horrors. I wish him strength through this time in his life.<p>
Also, just to add, if after you take a shower they have run out of towels, you instead get a "rag"--which usually means ripped underwear and T-shirts--to use. I'm entirely serious. <p>
A friend of mine wrote his congressman about the conditions at Atlanta. The result? The congressman called the warden, the warden denied it, and that was that. Some investigation.<br>
<b>Christopher Henderson</b>, Low Security Correctional Institution, Butner, N.C.<p>

