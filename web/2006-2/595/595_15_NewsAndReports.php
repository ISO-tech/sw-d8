<p><a name="MWSF"></a><font face="Times New Roman, Times, serif" size="4"><b>Midwest Social Forum</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Sherry Wolf</b></font><p>

<font face="Times New Roman, Times, serif" size="3">MILWAUKEE--Nearly 700 activists from across the Midwest registered for this year's Midwest Social Forum, July 6&#8211;9. Sponsored by the University of Wisconsin's Havens Center, <i>In These Times </i>and <i>The Progressive</i>, among others, it provided a forum for progressives to hash out political ideas and differences.<p>
Hosted in a city that had 40,000- and 70,000-strong immigrant rights protests in April and May, it was fitting that the weekend's highlights were the opening plenary and 15 sessions on immigrant issues. Useful debates emerged about what attitude to take toward the various "compromise" bills and the Democratic Party candidates who support militarization of the border and guest-worker plans. <p>
Two Arizona border activists, Ray Ybarra and Alexis Mazon, gave excellent presentations on organizing against the anti-immigrant Minutemen and how racist legislation by Democrats in the border states provides insight into why the movement should not accept compromise bills that would nationalize these laws.<p>
The Social Forum overall, however, lacked any perspective on ending the war in Iraq, fighting the siege of Palestine and many of the other challenges activists face today. <p>
Too many discussions on fighting racism degenerated into race baiting the mostly white crowd, rather than exploring how a multiracial movement could take on the attacks on people of color and working-class whites today.<p>

