<p><a name="stop"></a><font face="Times New Roman, Times, serif" size="4"><b>Stop the Nazis</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Brian Huseby</b></font><p>

<font face="Times New Roman, Times, serif" size="3">OLYMPIA, Wash.--About 500 antiracists turned out to protest a rally by the neo-Nazi National Socialist Movement (NSM) on the steps of the state capitol on July 3.<p>
The rally was billed as a "regional gathering" of the group's members from Washington, Oregon, California, Nevada and other Western states. Only 11 racists actually showed up. Even so, they were allowed to rally since they were protected by a chain link fence and 200 state troopers, who kept the protesters 200 feet away from the Nazis.<p>
When the NSM rallied at the same place three months earlier, more than 100 antiracist protesters were able to shout down the racists, who were separated by just a thin line of state cops. The cops were forced to lead the Nazis away after only about 10 minutes.<p>
Protester Adrienne Weller called the state troopers an "honor guard" for the neo-Nazis and pointed out that such protection is not offered to antiwar protesters.<p>
Anticipating that they would not be allowed near the NSM, protesters set up their own sound system and drowned out the Nazi message with chants, speeches and music. Protesters felt that their efforts were a success, as the NSM message was unable to be heard. <p>
"After a year and a half of planning, saying this was going to be a state and regional rally, they only got 10 or 12 people," said Robert Jacobs, regional director of the Anti-Defamation League. <p>

