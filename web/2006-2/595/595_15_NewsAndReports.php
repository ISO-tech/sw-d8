<font face="Times New Roman, Times, serif" size="4"><b>Stop Israel's assault on Gaza</b></font><p>

<font face="Times New Roman, Times, serif" size="3">SAN FRANCISCO--Outraged at Israel's ruthless siege on Palestinians in Gaza last week, some 400 people gathered to demonstrate at the Israeli consulate here on July 4. The demonstration--called by Al-Awda, the Palestine Right of Return Coalition--was one of the largest of several emergency responses across the country.<p>
"This is a great turn out, which we want to use to build awareness," said Charlie from the General Union of Palestine Students.<p>
About 40 pro-Israel protesters came to counterprotest but had little effect since pro-Palestinian rights activists outnumbered them 10 to 1. Al-Awda has called for another protest for July 13, the day before its annual convention begins at San Francisco State University.<p>
In New York, 150 protesters turned out for a July 6 protest and die-in in a park across from the United Nations. Chants of "From Iraq to Palestine, occupation is a crime!" and "Not another nickel, not another dime, no more money for Israel's crimes!" rang out. The next day, activists in Brooklyn collected donations that will go for medical and nutritional supplies to Gaza as part of an international day of action called by Al-Awda.<p>
Throughout the week, Palestinian rights activists organized events to voice their opposition to Israel's terror.<p>
In New Haven, Conn., 15 people picketed the office on June 29 of Rep. Rosa DeLauro, who voted to withhold economic aid to Palestinians after the democratic election of Hamas. In Toledo, Ohio, 15 protesters--including members of the Northwest Ohio Peace Coalition and the Nation of Islam--gathered on July 5 at the county courthouse with signs demanding "Justice for Palestine" and "End Israeli terror."<p>
In Chicago, 30 people held a lively picket at the Israeli consulate on July 5. About 35 protested at the Federal Building in Providence. In Burlington, Vt., activists from Burlington Says No to War, the ISO, Pax Christi and Vermonters for a Just Peace in Palestine and Israel picketed the office of Congressman Bernie Sanders, who voted in favor of the Palestinian Anti-Terrorism Act. <p>
<i>Craig Berman, Alden Eagle, Yusef Khalil, Elizabeth Lalasz, Jim Ramey, Alex Read and David Russitano contributed to this report</i>.<p>

