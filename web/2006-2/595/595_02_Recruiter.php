<font face="Times New Roman, Times, serif" size="4">Antiwar activists in New Haven refuse to be silenced</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Threatened by a Marine recruiter </b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Rebecca Lewis</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 14, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">ACTIVISTS WHO protested outside a recruitment station in New Haven, Conn., were threatened by a baseball bat-wielding Marine--but police said it was the activists who were "confrontational."<p>
The assault took place June 28 during a protest in which antiwar activists spread themselves along the edge of the sidewalk outside the recruitment office.<p>
When they began chanting, "They're our brothers, they're our sisters, we support war resisters," a Marine recruiter came to the door and was handed a baseball bat from inside. He proceeded down the line of activists, threatening them and pushing several with the bat. He then knocked a cell phone out of one person's hand, picked it up and took it inside with him.<p>
The activists had gathered outside the recruiting center the day before, in response to a national call to action to support Lt. Ehren Watada, the first officer to refuse to deploy to Iraq. They also aimed to draw attention to the case of Specialist Suzanne Swift, who refused to deploy to Iraq after she was sexually harassed by three of her commanding officers during her first deployment. <p>
Naturally, the small but spirited rally was met with skepticism by the staff at the recruiting office, but the interaction between protesters and recruiters remained peaceful, and even friendly. One Army recruiter jokingly invited the protesters back the next day, when their boss would be in town.<p>
The activists returned the next day with twice the numbers, though they took care not to obstruct doorways or the flow of traffic. That didn't stop the Marine recruiter, however.<p>
Police arrived moments after the recruiter took the cell phone, and they went first to the Marine's office. When they returned to the sidewalk, they immediately began accusing protesters of being "confrontational" and blocking the door. <p>
The Marine was allowed to return to work, drilling teenagers in the brick courtyard next to the station--while still holding the stolen cell phone. Police eventually returned the phone, but forced the demonstration across the street, telling protesters, "This isn't your sidewalk, this is my sidewalk."<p>
On July 5, about 70 people turned out to a press conference and rally called in response to this violation of the right to protest. Standing in front of the welcome sight of a closed recruiting station, Peter Goselin, of the National Lawyers Guild, told the crowd, "The front line in the defense of First Amendment freedom in this country is not in Baghdad, and it's not in Kabul. It's right here on this sidewalk and on sidewalks just like it all over the United States." <p>
Other groups represented at the press conference included the International Socialist Organization, Connecticut United for Peace, Iraq Veterans Against the War and the Campus Antiwar Network. <p>
The Marine recruiting office on Orange Street has since been closed pending an internal investigation.<p>
Activists in New Haven plan to continue campaigning for their right to speak out, and for the unconditional release of Ehren Watada, Suzanne Swift and all other war resisters. Rallies are planned in front of the recruitment station each Wednesday at 5:15 p.m., and an event is being planned for July 15 at 12:30 p.m. as part of a national call to action for Suzanne Swift.<p>
