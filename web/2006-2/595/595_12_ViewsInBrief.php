<font face="Times New Roman, Times, serif" size="4"><b>A fightback at Fletcher Allen</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NEGOTIATIONS FOR Fletcher Allen Nurses, AFT Local 5221, are underway, with our contract set to expire on July 9. With time running out, the hospital administration keeps telling us they're "not interested" in the majority of our proposals. <p>
The hospital's main interest appears to be getting us to give up many of the victories of our first contract. Our struggle is similar to that of most nursing unions across the country--fighting for patient safety, dealing with the nursing shortage and improving working conditions, wages and benefits. <p>
Our struggle is also unique. To begin with, we are uniting our registered nurses (RNs) and licensed practical nurses<b> </b>(LPNs) in one contract. This is unusual due to National Labor Relations Board regulations, and it is one small step toward unifying all workers in the hospital. <p>
The hospital continues to do all it can to divide us, just as the ruling class tries to do whatever it can to pit worker against worker. In our first contract negotiations, the hospital focused on dividing RNs and LPNs. <p>
This time around, they seem focused on dividing nurses from other hospital workers. An example of this is that the hospital told us they'd prefer to focus on wages because then they can give us more without having to give other workers more, rather than improving our benefit package, which would improve conditions for all workers at the hospital. <p>
The hospital does not see that for many of us, our real goal is to do whatever we can for all workers, especially those who are most oppressed. Just as we struggled in our first RN negotiations to do whatever we could to get the LPNs a fair contract, this time we are looking to the future and the continued struggle to organize all our workers.<p>
Local 5221 is also the largest union in Vermont, and because of this, we recognize the importance of winning a good contract to help lead other unions in our state to bigger victories. We have the power and the responsibility to stand up, not just for our own rights, but for all unions and workers in Vermont. <p>
Some see our union as one of the most radical in the state. I hope that we will live up to this image in this next week as our old contract expires. <p>
Many of our rank-and-file nurses are ready to do whatever is necessary to maintain and build on the strengths of our first contract. I know that we will continue to unify within our rank and file so that each step of the way we will have a stronger voice for all workers.<br>
<b>Christina V. Neumann,</b> Local 5221 bargaining team, Burlington, Vt.<p>

