<p><a name="Fraud"></a><font face="Times New Roman, Times, serif" size="4"><b>Ignoring the real fraud</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THE CONGRESSIONAL Government Accountability Office (GAO) has released a report concluding people filing false claims with FEMA have received up to $1.5 billion in undeserved aid for Hurricane Katrina. <p>
Anecdotal examples of abuse contained in the report include relief money spent on football tickets and dining at Hooters, as well as some larger scams involving tens of thousands of dollars. Republican Rep. Michael McCaul, chair of the House subcommittee that initiated the investigation, finds the fraud "shocking and appalling." <p>
What hypocrisy! The fraud uncovered by the GAO pales in comparison to the rampant fraud perpetrated by the U.S. government. <p>
According to the Center for Public Integrity, over the past five years, members of Congress have received over $50 million in vacations paid for by lobbyists. This figure doesn't even include the billions of dollars in bribes (aka "campaign finance") that lobbyists and corporations lavish on both Democrats and Republicans. <p>
How many members of Congress have enjoyed Jack Abramoff's hospitality suites that included "service" of a qualitatively different nature than what's served up at Hooters?<p>
The scale of Katrina fraud is dwarfed by the $95 billion Congress just appropriated to continue funding the Iraq and Afghanistan wars--two of the greatest frauds ever perpetrated by the U.S. government. <p>
Also ignored by the GAO report are the fates of tens of thousands of people who haven't received help, people who are scattered around the country with no prospects for employment, people stranded in "Femaville" trailer parks, people unable to return to their homes and others being evicted from their homes.<p>
This minor fraud is a cheap political stunt to distract from the real crimes of Congress. The loss of these funds isn't what's preventing people from receiving help--our government's disgusting and twisted spending priorities are responsible for that.<br>
<b>Nicholas Hart,</b> Seattle<p>

