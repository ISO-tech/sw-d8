<p><a name="Rts"></a><font face="Times New Roman, Times, serif" size="4"><b>Standing up for gay rights</b></font><p>

<font face="Times New Roman, Times, serif" size="3">THE PEOPLE of Washington have rejected a petition drive to repeal a recently passed gay civil rights bill. <p>
In January, by a 25 to 23 vote, the state legislature passed a law that would prohibit discrimination against lesbian, gay, bisexual and transgendered (LGBT) people in employment, housing and lending, making Washington the 17th state to extend such rights.<p>
Immediately after passing the law, LGBT people were once again under attack when anti-tax initiative organizer Tim Eyman and a network of conservative evangelical organizations, including the Faith &amp; Freedom Network and the Christian Coalition, joined forces to begin a petition drive to place a referendum on the November ballot to recall the civil rights law. <p>
Despite a well-funded and well-organized antigay propaganda campaign and a "Referendum Sunday" effort to collect signatures at churches throughout the state, organizers fell short of the required 112,440. <p>
The failure of the referendum effort forced organizers to reconsider their alliance of convenience. The evangelicals were not impressed with Eyman's arrogant showboating, including showing up to the press conference with the results of petition drive in a Darth Vader costume.<p>
Washington's civil rights bill quietly went into effect on June 7. The defeat of antigay bigotry in our state should send a clear message to the people of Washington that the struggle for civil rights for everyone, including marriage equality, enjoys the support of the majority of the state's population. Gay rights organizations can now look to this positive public support to reinvigorate and mobilize the fight for marriage equality.<br>
<b>Lonnie Lopez,</b> Seattle<p>

