<font face="Times New Roman, Times, serif" size="4">Woman soldier faced repeated sexual abuse in Iraq</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>"Mom, I just can't go back"</b></font> <p>
<font face="Times New Roman, Times, serif" size="2"><b>By Evan Kornfeld</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | July 14, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">POLICE IN Eugene, Ore., arrested Suzanne Nichole Swift June 11 and charged her with desertion for the "crime" of refusing to return to Iraq.<p>
Swift, a 21-year-old specialist serving with the 54th Military Police Company, explained her decision by citing not only her opposition to the illegal and immoral war in Iraq, but also repeated sexual harassment and abuse suffered at the hands of her commanders and other soldiers.<p>
In January, Swift was ordered to return to Iraq with her unit, but she did not report for duty. <p>
When she joined the Army in spring 2003, recruiters told Swift that if she signed up as a military police specialist for a five- instead of a four-year stint, she would not be sent to Iraq. When she arrived at basic training, she was told immediately that she would be sent to Iraq.<p>
According to Swift, after her deployment in January 2004, three sergeants began demanding sexual favors. One would show up drunk at her door in the middle of the night, demanding to have sex. "He's just insane, mom," Swift told her mother. "He's an insane person, and I'm scared to death." <p>
Swift says that she had no choice but to have a relationship with one of the sergeants. "In a combat situation, your squad leader is deciding whether you live or die," she explained to the (Eugene) <i>Register-Guard</i>. "If he wants you to run across a minefield, you run across a minefield."<p>
Swift reported the abuse to her unit's equal opportunities officer, but nothing changed. Her unit returned to the U.S. in February 2005. <p>
Once, when she asked a sergeant where she should report then next morning, he replied in front of a group of soldiers, "In my bed--naked." Swift then decided to press sexual harassment charges against him. The sergeant was transferred to another unit, but Swift says she was treated terribly after this. <p>
In January 2006, she was told she was going to be sent back to Iraq. This was only 11 months after her return to the U.S.--and Swift was forced to sign a document waiving her right to 18 months' decompression time. <p>
Swift prepared for her return, but changed her mind three days before her deployment. "She was in the kitchen, and we were looking at each other," recalls Swift's mother, Sara Rich. "She turned to me, and she said, 'Mom, I just can't go back.'"<p>
Swift stayed for a time with a friend in southern Oregon, and in May, she returned to Eugene to live with her mother. Swift has been diagnosed with post-traumatic stress disorder, and the Army has not said whether it will try her for desertion. <p>
As a result of these experiences, Swift's mother has become involved in the counter-recruitment movement and has heard many stories of abuse similar to her daughter's from women who have served in the military. She says her daughter has told her, "Mom, I want to join you as soon as I'm clear. I want to join you and tell kids what the recruiters are really doing. It's really like selling your soul to the devil to be human fodder for an illegal war."<p>
