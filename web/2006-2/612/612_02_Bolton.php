<font face="Times New Roman, Times, serif" size="5"><b>Another Bush hawk bites the dust</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 8, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">ANOTHER BUSH administration head has rolled. John Bolton, the hard-line right winger who George Bush sent to be U.S. ambassador to the United Nations (UN), notified the White House that he was stepping down as <i>Socialist Worker </i>went to press.<p>
Bolton was an unpopular choice for the job even among some Republicans, and was never able to win Senate confirmation. Bush used a so-called recess appointment last year to get him the post, but the appointment expires at the end of this month, and Bolton's resignation acknowledged that administration attempts to ram his nomination through the Senate before control shifts to the Democrats next year are doomed.<p>
Bolton was the perfect choice for the Bush administration's hard-line hawks to display their contempt for the United Nations and the governments of other countries, even longtime allies. Among other things, Bolton was notorious for once saying, "The [UN] Secretariat building in New York has 38 stories. If it lost 10 stories, it wouldn't make a bit of difference."<p>
Bolton made a career out of bashing the UN--and rejecting the idea that world opinion should ever constrain the U.S. from pursuing its military or economic aims. As <i>Nation</i> reporter Ian Williams told <i>Democracy Now! </i>when Bolton was appointed, "John Bolton is to diplomacy what Jack the Ripper was to surgery."<p>
When Bolton took the job as UN ambassador, he announced, "We seek a stronger, more effective organization, true to the ideals of its founders and agile enough to act in the 21st century." That translated into barely concealed threats and attempts to bully other nations into total agreement with the U.S.<p>
During his short time as ambassador, Bolton threatened Iran with "tangible and painful consequences" for pursuing a nuclear program--and once blocked a United Nations envoy from briefing the UN Security Council on rights violations in the Darfur region of Sudan.<p>
Last year, during a debate about the future division of authority at the UN, Bolton commented, "The UN is one of many competitors in a marketplace of global problem solving," and threatened that the U.S. might "need to find another organization to accomplish our objectives" if the UN did not fall in line with U.S. demands.<p>
Oswaldo de Rivero, the UN ambassador for Peru, told Bloomberg News earlier this year that Bolton "lives in another world, with this belief that he is morally superior, and the U.S. is more moral than all the countries around the world." It's no wonder, then, that many UN officials are reportedly delighted that Bolton is out.<p>

