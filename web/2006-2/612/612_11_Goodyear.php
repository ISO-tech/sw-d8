<font face="Times New Roman, Times, serif" size="4">Management pushes union-busting demands despite strong profits</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Strikers at Goodyear dig in</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Marshall Braun</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 8, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">SUN PRAIRIE, Wis.--A strike by 250 Goodyear workers here--part of a walkout of 12,000 union members nationwide--is now entering its third month.<p>
United Steel Workers (USW) Local 904 members at two plants in Sun Prairie went on strike against Goodyear--the largest tiremaker in the U.S.--on October 5 after rejecting a company offer that would cut benefits and salaries.<p>
Demanding concessions is hardly new for Goodyear, which forced workers to accept concessions in 1997 and 2003 to keep the company profitable. Goodyear is now turning record profits but is asking for even more concessions from its workforce.<p>
On November 2, 350 union members and supporters joined a mass picket line in solidarity with Local 904 members. Representatives from at least seven area unions came to show their support. David Newby, president of the Wisconsin AFL-CIO, was at the picket and pledged to aid the strikers until the end of the strike.<p>
The large crowd was boisterous with chants of "What's disgusting? Union busting!" and "Contract now!" The two-hour action dramatically slowed down scab workers and management as they left the plant.<p>
A second mass picket, organized by the South Central Federation of Labor, is planned for December 7.<p>
Since the beginning of the strike, workers have run 24-hour, seven-day-a-week pickets. Goodyear has hired a local strikebreaking company to transport scabs by van to staff two 12-hour shifts, yet the union says that production is still running at a fraction of pre-strike levels.<p>
Despite Goodyear's offer to workers of pre-strike pay and benefits to cross the picket line, only one union member has done so. They are standing strong despite being given only coupons for $60 in groceries and $30 in gas per week by the International after waiting for nearly four weeks for the strike fund to kick in at all.<p>
Workers lost their dental care insurance the day they left the plant. Their health insurance is scheduled to run out on January 3, and those who would like to maintain their coverage would have to pay upwards of $600 a month.<p>
Negotiations between the union and Goodyear broke off again mid-November when Goodyear refused to budge on its demands to increase health care premiums, cut wages and retirement benefits and close unionized plants like one in Tyler, Texas, which they put on the chopping block after the strike began.<p>
In addition to cuts for current workers, the latest offer would create a six-tier wage structure and dramatically cut most benefits for new hires. Other gems, like one that staggers contract endings between individual plants, have been snuck in to the company's proposal in order to weaken the union's future collective bargaining power.<p>
