<p><a name="Dems"></a><font face="Times New Roman, Times, serif" size="4"><b>The Democrats in charge?</b></font><p>

<font face="Times New Roman, Times, serif" size="3">NOW THAT the Democrats will control Congress, they can stop appeasing the Bush war administration. There might even be separation of powers, and checks and balances in the federal government.<p>
But if the Democratic Party really wants to change course, then it should stand against torture, extraordinary renditions, executions, secret trials and domestic spying of innocent Americans. We need a government that supports human rights and civil liberties.<p>
We might get change, or we might get the same old politics as usual.<br>
<b>Chuck Mann</b>, Greensboro, N.C.<p>

