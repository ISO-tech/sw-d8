<font face="Times New Roman, Times, serif" size="4"><b>What's next in Oaxaca?</b></font><p>

<font face="Times New Roman, Times, serif" size="3">AVERY WEAR is correct to point out the importance of the People's Assembly of Oaxaca (APPO), not merely as a protest movement, but as an example of the power of organized workers in one of the most impoverished states of Mexico ("Power in Oaxaca," November 17).<p>
However, comparing the situation in Oaxaca to the Paris Commune or to the Petrograd Soviet in 1917 is misleading. Despite APPO's impressive strength on the streets, and the involvement of many different sectors of the population in the struggle, including important transportation unions, only the teachers have used workers' most potent weapon, namely, the strike.<p>
There are other important differences, too. First, both the Paris Commune and the Petrograd Soviet succeeded in breaking the ruling class' monopoly of armed power. Unfortunately, today, this is all too clearly not the case in Oaxaca.<p>
Second, both Paris and Petrograd were the economic and political nerve centers of their respective countries. Workers' power in both cities posed a real and present danger to capitalism on a national level. Certainly it is not a criticism of APPO to point out that Oaxaca stands in a different relation to Mexican capitalism.<p>
Finally, in both Paris and Petrograd, there were conscious and organized political currents or parties who fought to link the immediate struggles they waged to the goal of abolishing capitalism. It is not at all clear what degree of strength, if any, similar forces have within APPO.<p>
Socialists should learn the lessons of Oaxaca and organize concrete solidarity for the struggle, but we must also, to paraphrase Trotsky, not mistake the first month of pregnancy for the ninth. To argue that the struggle in Oaxaca is "a real 21st century socialist revolution," as Avery does, is to forget that real workers' power in Mexico can only be achieved when the tens of millions of industrial, transportation, extractive, service and state workers in the great metropolises move into action in their own interests and take the lead in fighting for the just demands of all the oppressed and exploited.<p>
While pointing to all the positive lessons APPO has to teach the international working class, it is important to point out that, despite heroic and militant struggle, the Mexican working class still lacks even the rudimentary organization of a nationwide revolutionary socialist party. Certainly the conditions exist to change this, but wishful thinking will not help overcome the very real political obstacles on the Mexican left today.<br>
<b>Todd Chretien</b>, Oakland, Calif.<p>

