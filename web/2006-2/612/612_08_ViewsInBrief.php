<p><a name="Wrong"></a><font face="Times New Roman, Times, serif" size="4"><b>Wrong on recruitment?</b></font><p>

<font face="Times New Roman, Times, serif" size="3">I AM a military recruiter. I don't disagree with everything I have read in your paper, but some of this stuff is incorrect. I think that you are being misled and misinformed on some things.<p>
First of all, we don't go after just the poor--that would be stupid. There are always people in upper middle-class schools that want to join. I have gotten several young men that come from upper-class families.<p>
Second, college money is an absolute. The Montgomery G.I. Bill gives $42,000 in college money to recruits. This money can be used for 10 years after you leave the military. Yes, you pay $100 a month for 18 months into the G.I. Bill--but that's how the military gets the funds to pay for college. I think $1,800 is quite small compared to what you get back. Also, there are many non-combatant jobs in the military.<p>
We are a business just like any other business. We decided who we want, not you! If you would support us rather then resist us than maybe our jobs would not have to be so difficult.<p>
So it comes down to this: If you are going to talk about military recruitment, then get all of your facts straight. The military must stay strong, and we can do it one of two ways: one, allow people to volunteer, or two, the draft. Which would you rather have?<br>
<b>Christopher Treichel</b>, from the Internet<p>

