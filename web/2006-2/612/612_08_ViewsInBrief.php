<p><a name="Right"></a><font face="Times New Roman, Times, serif" size="4"><b>Standing up to the right</b></font><p>

<font face="Times New Roman, Times, serif" size="3">ON NOVEMBER 6, several members of the Washington, D.C., branch of the International Socialist Organization (ISO) witnessed firsthand the audacity of racists in the current mainstream acceptance of hate.<p>
During a sale of <i>Socialist Worker</i> at the University of Maryland, a sign reading "From Iraq to Palestine, occupation is a crime" came under ridicule. A student took out a giant Israeli flag (which he happened to be carrying with him) and began protesting our sale. Within minutes, many others joined him.<p>
Though the sale was intended to address the issues surrounding the election, it soon was turned into a concerted attack by the crowd of Zionists and right wingers.<p>
The debate not only displayed an open disregard for Palestinian rights and people, but a malicious racist ideology towards Arabs and Muslims. One person proclaimed, "You can't trust Georgetown, it's like 80 percent Arab." A Jewish member of the ISO was attacked for "not being Jewish enough" because he supported Palestinian rights, and another person said, "All Arabs are terrorists."<p>
Not intimidated, we stood our ground despite being greatly outnumbered, and called the bigots out. Several students approached us during the course of the discussion and thanked us for standing up against hate.<p>
And we wouldn't let the struggle end there. What happened clarified to us the necessity for an immediate and vocal declaration that there were students on campus who would not be afraid to stand up for Palestinian rights, and to fight Islamophobia and anti-Arab racism.<p>
Within a week, we organized a rally to raise campus consciousness about these issues. We worked with the Muslim Student Association and the campus antiwar group, and with their help, the rally was a rousing success. More than 50 students came out, silencing the bigots who had so arrogantly proclaimed hate the week before.<p>
One ISO member gave an inspiring speech, referencing the recent Israeli attacks on Beit Hanoun, and urged all those present to stand in solidarity and struggle against the forces of hate and imperialism.<p>
The power of solidarity was ultimately demonstrated by the various elements of the left that had come together, establishing a precedent which will hopefully continue in the future. We should celebrate this success, and make sure that we will continue to be there on the front lines, fighting the good fight.<br>
<b>Tim Burt</b>, Washington, D.C.<p>

