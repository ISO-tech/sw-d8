<font face="Times New Roman, Times, serif" size="5"><b>Blaming the victims for Iraq's nightmare</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Sharon Smith</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 8, 2006 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">FOR MORE than a century, the U.S. claimed each time it invaded another sovereign nation that it was selflessly shouldering the responsibility of "civilizing" a backward population.<p>
This process became widely known as "the white man's burden" after Rudyard Kipling's 1899 poem of the same name, which described the conquered populations as "sullen peoples, half-devil and half-child."<p>
Kipling's poem celebrated the 1898 U.S. invasion of the Philippines, which killed well over half a million civilians in the following years. The U.S. government crushed the Filipino insurgency--and refused to grant independence to the Philippines until 1946.<p>
In Iraq, the U.S. has managed to kill a similar number of Iraqis, but failed to crush the resistance. The Washington establishment (minus the increasingly isolated and delusional Bush and Cheney) has finally concluded that the Iraq war is "unwinnable," and the imperial endgame is beginning.<p>
Commitments to "bipartisanship" and "compromise" are already echoing through the halls of Congress, as Democrats and Republicans unite to salvage what remains of U.S. imperialism's longstanding aims in the Middle East.<p>
Democrats and Republicans have joined together to take aim at the ungrateful Iraqi population, who apparently fail to appreciate the U.S.'s selfless efforts to impose "democracy" through military occupation. On this point, the two parties are indistinguishable.<p>
As the <i>Washington Post</i> reported, "[A] November 15 meeting of the Senate Armed Services Committee turned into a festival of bipartisan Iraqi-bashing."<p>
<blockquote>
"We should put the responsibility for Iraq's future squarely where it belongs--on the Iraqis," argued Democratic Sen. Carl M. Levin, who will chair the committee in January. "We cannot save the Iraqis from themselves."<p>
"Sen. Lindsey O. Graham (R-S.C.) followed by noting: "People in South Carolina come up to me in increasing numbers and suggest that no matter what we do in Iraq, the Iraqis are incapable of solving their own problems through the political process and will resort to violence, and we need to get the hell out of there."
</blockquote><p>
New House Speaker Nancy Pelosi chimed in, quoted in the <i>Congressional Quarterly</i>, "We need to send a message to Iraqis that our patience is not unlimited." Likewise, presidential wannabe Sen. Barack Obama stated that there should be<i> "</i>[n]o more coddling" of Iraqis.<p>
Within a few short weeks, the Washington "consensus" has rewritten the history of the U.S. invasion of Iraq--as if Iraqis invited the U.S. to invade their sovereign nation in 2003 and now have failed to live up to their end of the bargain. The mass civilian bloodshed at the hands of the U.S. military is apparently irrelevant in this equation.<p>
The war was based on a set of lies. So, too, is today's talk that a "phased withdrawal" constitutes a genuine withdrawal, prefaced by the clumsy attempt to blame Iraqis for the state of their country.<p>
The much hyped report of the Iraq Study Group (ISG)--itself a product of bipartisan "consensus"--is anticipated to call for halving the number of U.S. forces in Iraq. But that still leaves 70,000, redeploying to U.S. bases inside Iraq or just outside. The ISG is expected to call for troop withdrawals, but without any firm deadline. As Phyllis Bennis of the Institute for Policy Studies observed, the ISG holds "virtually the same position as Bush's own 'when Iraqis stand up we will stand down.'"<p>
The electorate expressed its opposition to the Iraq war on November 7. But electoral opposition is clearly not enough to convince the two war parties in power that U.S. troops must leave Iraq--and should never have invaded in the first place. U.S. occupation has brought nothing but violence to the Iraqi people, and will do nothing to stem the bloodshed now.<p>
Instead of blaming Iraqis for the misery that U.S. occupation has brought them, U.S. lawmakers should listen to them. A September opinion poll by WorldPublicOpinion.org showed that 71 percent of Iraqis want the U.S. out of Iraq within a year.<p>
The long dormant antiwar movement must take to the streets to remind this country's ruling elite that they ultimately must answer to the people they govern.<p>

