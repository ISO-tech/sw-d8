<font face="Times New Roman, Times, serif" size="5"><b>Banned from a flight for praying</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Nicole Colson and Chris Yarrison</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 8, 2006 | Page 2</font><p>

<font face="Times New Roman, Times, serif" size="3">ANTI-MUSLIM racism was on full display last month when six Muslim religious leaders were thrown off a US Airways flight in Minneapolis. Their crime? The imams were first deemed "suspicious" for observing their evening prayers in the airport, prior to boarding the flight.<p>
But this latest incident of racial profiling in U.S. airports has sparked anger, leading to "pray-ins" by Muslim, Jewish and Christian leaders at several airports--and a demonstration outside the headquarters of US Airways to demand an apology.<p>
The six imams were returning home November 20 after attending a Minneapolis conference of the North American Imams Federation. Passengers and crew members reportedly became alarmed by the imams' prayers. Others told the media that the men had "switched seats" into a pattern "reminiscent" of the September 11 hijackers.<p>
Police were called in after a passenger reportedly slipped a note to a flight attendant that read "6 suspicious Arabic men on plane." The imams were handcuffed and led off the flight.<p>
"They took us off the plane and humiliated us in a very disrespectful way," one of men, Omar Ahmad Shahin, told the Associated Press. "I never felt bad in my life like that," he added. "I never. Six imams. Six leaders in this country. Six scholars in handcuffs. It's terrible."<p>
The six were held for five hours and questioned, before finally being released. Yet despite their innocence, US Airways refused to place them on another flight--or even find housing for the night for them. According to the Muslim American Society (MAS) Freedom Foundation, the airline "seemed to have created an atmosphere that resulted in, for over 10 hours, all airlines refusing to sell them tickets to return home."<p>
After the incident, a US Airways spokeswoman, Andrea Rader, refused to apologize, telling the Associated Press that "crews have to make these calls, and we think they made the right one."<p>
Right-wing radio blowhards like Michael Graham picked up on the line. "[D]o even the most liberal among us find it unreasonable for leaders of the only major world religion with an ongoing terrorism problem to pay a price for the shameful state of their faith?" Graham wrote in an opinion article titled "US Airways be praised for safety" in the <i>Boston Herald</i>. "After all, these imams weren't attending a conference on 'How To Rescue Islam From Radicals.' No, it was yet another gathering of Muslims, whining about their victimhood."<p>
The phenomenon of "flying while Muslim"--Muslim and Arab passengers subjected to racial profiling by airlines, particularly since the September 11 attacks--is on the rise. American, United, Delta and Continental have all been found by the Transportation Department to have unlawfully removed passengers for their perceived religious or ethnic backgrounds.<p>
Worse, a Gallup poll this summer found that 39 percent of Americans favored requiring Muslims in the U.S.--American citizens and not--to carry special identification.<p>
As Ahmed Rehab of the Council on American-Islamic relations pointed out in a statement, "At what point does prayer become 'suspicious activity' that warrants handcuffs, detention and FBI investigations? It is not so surprising when you consider the barrage of polemical commentary fed to the public on a daily basis, where Muslims and Islam are made synonymous with terrorists and terrorism."<p>
This is why the determination to stand up to racism in the US Airways case is so important.<p>
The NAACP has joined the MAS Freedom Foundation and other groups in support of a bill sponsored by Rep. John Conyers' (D-Mich.) to prohibit racial and religious profiling. Leaders of different religions came together for the "pray-ins" held at several airports, including at Reagan National Airport in Washington, D.C., and Minneapolis-St. Paul International Airport.<p>
More than 100 people followed up December 1 with a rally outside US Airways' corporate headquarters in Arizona to call for an apology to the six imams. "Deliberate injustice is more fatal to the one who imposes it than on the one on whom it is imposed," Mahdi Bray, executive director of the MAS Freedom Foundation, told the demonstrators. "We want to tell US Airways that second-class citizenship is not an option."<p>

