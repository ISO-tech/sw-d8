<font face="Times New Roman, Times, serif" size="5"><b>Rebuilding the struggle</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 8, 2006 | Page 5</font><p>

<font face="Times New Roman, Times, serif" size="3">MARGARITA ROSARIO'S son Anthony was killed by New York police in 1995--shot 14 times while he lay facedown in a Bronx apartment. The NYPD claimed Anthony and his cousin Hilton Vega were killed in a shootout that they started after a failed robbery.<p>
But when Margarita and Hilton's mother Carmen Morales couldn't get answers from police, they began investigating themselves--and they founded Parents Against Police Brutality, an organization that has led the struggle to protest police brutality in New York. Margarita talked to <i>Socialist Worker </i>about the fight in the wake of Sean Bell's murder.<p>
<p><font face="Arial, Helvetica, sans-serif" size="1">- - - - - - - - - - - - - - - -</font></p>

<font face="Times New Roman, Times, serif" size="3"><b>HOW DID you get started organizing?</b><p>

I DON'T remember Martin Luther King personally, but I grew up seeing him on TV, and I remember clearly learning about the struggle he was involved in, and the attacks the police made on people who were marching for their rights.<p>
I grew up knowing that there are two classes of people. There are the people who are poor and struggling, and there are the people who don't care about human life. I was always an outspoken person. I also spoke out about police brutality before anything happened with my son.<p>

