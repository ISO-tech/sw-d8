<font face="Times New Roman, Times, serif" size="3">When Anthony Baez was killed [by New York police in 1994], my son said to me, "You know ma, if I was that guy's family, I would go after them with everything I got." Two-and-a-half weeks later, my son was killed.<p>
When they told me that my son had a shootout with the cops, I knew immediately I wasn't going to sit on this. Family members were totally against me taking a stand, but I felt I had to do this. I had to speak for my son, since he no longer had a voice.<p>
If the medical examiner had made me feel okay about what had happened, perhaps I would have stopped there. But to go there to talk with the person who did the autopsy, and to find out that the person was suddenly on vacation for 30 days? And no one has the keys?<p>
I knew right away something was wrong. I wanted to see the facts. Since I didn't get the facts, I decided to pursue this further.<p>
So I decided to form Parents Against Police Brutality. We contacted different organizations like the October 22 Coalition, Congress of Puerto Rican Rights, we contacted Al Sharpton, and through all of them, we were able to get in contact with other parents.<p>
We started building up a movement. It just developed overnight. It was fantastic to be among people who understand your pain, just crying on each other's shoulders. The pain is so severe.<p>

<b>CAN THIS movement make a difference?</b><p>

IT WAS the commissioner and the mayor who took a step back [after the protests against Abner Louima's torture]. Without them doing that, the police will always be the police.<p>
We need to learn from the past--from things we did in the past, how we approached it, and what came out of it. Diallo was shot 41 times. What came out of it? Did anything change? They used hollow-point bullets in my son, and they used hollow-point bullets to shoot Diallo.<p>
Marc Ferguson was killed two or three weeks after Diallo. So what changed? Nothing. Why? Because we concentrated on the 41 bullets. We didn't concentrate on bringing change in the police department. We made changes in people's minds, perhaps, but not yet in the department. We made people aware of police brutality, but what change came about?<p>
I'm proud of the movement, because we did see some changes as far as people getting involved, and there were a lot of talk shows on TV about police brutality. That was excellent. But we need to do more right now. This is a case that could bring some serious change.<p>

