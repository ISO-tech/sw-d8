<font face="Times New Roman, Times, serif" size="5"><b>One year after Stan Tookie Williams' execution</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Terzakis</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 8, 2006 | Page 4</font><p>

<font face="Times New Roman, Times, serif" size="3">DECEMBER 13 will mark the one-year anniversary of the murder of Stanley Tookie Williams by the state of California.<p>
Williams, who co-founded the Crips street gang in the 1980s, was sentenced to death for four 1979 murders that he maintained he did not commit. He transformed himself on death row and dedicated the rest of his life to convincing young people to fight for justice in a racist society rather than join the street gangs that are an outgrowth of that society.<p>
A TV movie starring Jamie Foxx spread Stan's message further, and he was nominated for Nobel peace prize. Thousands of people in California and across the U.S. participated in the struggle to save Stan, attending rallies, marches and forums where celebrities such as Snoop Dogg and Danny Glover spoke out.<p>
On the night of the execution, an estimated 5,000 people crowded into the dark and narrow street outside San Quentin Prison to protest. Barbara Becnel, Stan's longtime co-author and advocate, witnessed his execution and left the execution chamber with her fist raised, saying, "The state of California just killed an innocent man."<p>
Stan's execution has had a continuing impact on the death penalty system in California. Becnel said that what she witnessed in San Quentin's execution chamber was nothing like the "humane," quasi-medical procedure the state promises.<p>
Instead, she said, Williams was tortured to death. Once the drugs started flowing, Becnel told audiences during a recent national tour with other witnesses to executions, Williams' body writhed and contorted--so much so that she stopped praying for a last-minute reprieve, began wishing that his death would come quickly.<p>
The mainstream press and state officials dismissed Becnel's accusations of state torture as "hysterical." But in February 2006, Judge Jeremy Fogel halted the execution of Michael Morales in part because he found Becnel's claims were credible--and bolstered the contention of Morales' attorneys that the state's three-drug lethal injection "cocktail" constituted cruel and unusual punishment. Since then, California has enjoyed a de facto moratorium on executions.<p>
On December 12, Becnel and other anti-death penalty activists will mark the anniversary of Stan's murder with a program at the Black Repertory Theater in Berkeley. At midnight, the audience will witness what happened in the execution chamber a year earlier. Event organizers hope the program will convince more people that there is no way to humanely kill a human being--and that the de facto moratorium that came too late for Stan should be made permanent.<p>
