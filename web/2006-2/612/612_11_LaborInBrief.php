<font face="Times New Roman, Times, serif" size="5"><b>On the picket line</b></font><P>
<font face="Arial, Helvetica, sans-serif" size="2">December 8, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="4"><b>Teamster election</b></font><br>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><p>

<font face="Times New Roman, Times, serif" size="3">JAMES HOFFA has won re-election as president of the International Brotherhood of Teamsters on the basis of big campaign election spending and a low turnout by a cynical and demobilized membership.<p>
Hoffa's challenger, reformer Tom Leedham, took 35 percent of the vote in a campaign in which he was outspent by a 10-1 margin by Hoffa--$3 million to $300,000. The tally was 172,930 votes for Hoffa to 91,706 for Leedham, out of a membership of about 1.4 million.<p>
Certainly Hoffa's victory isn't on the basis of making big gains for Teamsters. Hoffa presided over the disastrous strike at Overnite Transportation in a failed bid to organize drivers at the major freight carrier. Then Overnite was purchased by UPS, the largest Teamster employer, giving the company a big boost in its efforts to expand the nonunion portion of its business.<p>
Meanwhile, Teamster representatives on pension boards rolled over for employer demands to cut pension benefits--even in the Western region pension board, where the system was fully funded.<p>
An analysis on the Teamsters for a Democratic Union (TDU) Web site points out, "Teamsters in the Central and Southern regions who were hit with pension cuts voted for Leedham in higher numbers. "Hoffa's victory margin in these areas--and across the country--was padded by votes from locals where members aren't affected by his contracts and benefit cuts and by the votes of Teamsters from the 150,000 members of the recently merged graphic communications and rail unions."<p>
The reform movement is also still working to recover from the ouster of former Teamsters President Ron Carey, who led the 1997 UPS strike. Carey stepped down and was later expelled from the union on charges of corruption, although he was later vindicated in federal court.<p>
The Teamster reform movement regrouped behind Leedham's candidacy in the special election in 1998, and again in 2001 and 2006.<p>
In the meantime, however, the reform movement lost a longtime bastion of support among Northwest Airlines flight attendants, who decertified the union in response to Hoffa's intervention in their local.<p>
Hoffa's operatives also wrecked Local 556 at the Tyson meat processing plant in Pasco, Wash., a mostly immigrant workforce led by reformer Maria Mart&iacute;nez, a co-chair of TDU. Hoffa's candidate in an election challenge to Mart&iacute;nez in 2002 then collaborated with management to help the company win a union decertification election last year.<p>
Despite all this, Leedham, who was the International's warehouse director under Carey, was able to use his campaign to connect with rank-and-file activists across the U.S. With contracts at UPS and other major employers coming up, this network will be key to taking the union forward.<p>

