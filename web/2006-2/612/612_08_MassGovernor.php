<font face="Times New Roman, Times, serif" size="4">Democrat Deval Patrick wins Mass. governorship</font><br>
<font face="Times New Roman, Times, serif" size="5"><b>Can we expect a change?</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">December 8, 2006 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">THE VICTORY of Democrat Deval Patrick in the race for governor of Massachusetts made for a celebratory mood all across Boston on November 8.<p>
In a city with a notorious history of racism, Patrick's landslide win--making him the first Black governor in Massachusetts history and the second anywhere in the U.S. since Reconstruction in the 19th century--was an inspiration. Even the right-wing, racist <i>Boston Herald</i> (begrudgingly) printed a smiling Patrick on its front page over the headline, "HISTORY!"<p>
Without a doubt, the victory of a Black liberal governor marks a step forward and speaks to the level of rejection and disgust with the Republican Party, which has dominated the U.S. Congress since 1995 and the Massachusetts governorship since 1991. But people in Massachusetts who expect the former Coca Cola executive to deliver relief from the right-wing attacks on our rights and the disastrous jobs situation for working people--192,000 jobs have left Massachusetts since 2000--shouldn't hold their breath.<p>
Patrick's campaign was remarkably good at raising hopes without promising much at all. While Patrick did run on a platform that included support for stem-cell research and raising the minimum wage, most of what we got from his campaign was the vague and vacuous slogan "Together we can."<p>
When Republican candidate Kerry Healy ran disgusting attack ads associating Patrick with a convicted rapist, Patrick seized the opportunity to turn his entire campaign into one "against negativity," instead of one about issues that matter in the lives of people.<p>
While Patrick said that he supported gay rights in media interviews, his positions were nowhere to be found on his campaign Web site. Thus, on October 15, when outgoing Republican Gov. Mitt Romney gave a nationally televised sermon against same-sex marriage at the Tremont Baptist Church, Patrick spoke at the same time at a so-called "Rally for Change," two blocks away at the Boston Common, and didn't mention gay marriage once.<p>
The weekend before Election Day, Sen. Barack Obama (D-Ill.) came to Roxbury to stump for Patrick. The two men have much in common--both promoting a pro-business Democratic Party agenda half-heartedly disguised with rhetoric of "the politics of hope."<p>
In Patrick's acceptance speech, he made sure to tone down expectations by underscoring the point that his campaign was not a liberal one. Speaking to the thousands of campaign staffers who came to celebrate his victory, Patrick said, "Every kind of person is here...You are Democrats and Independents and Republicans. You are liberal and moderate and conservative."<p>
Now that the right wing in Massachusetts is licking its wounds and our side is feeling a boost, progressives here need to seize the moment to rebuild once-vibrant movements for same-sex marriage and against the war--movements that collapsed into John Kerry's campaign for president in 2004 and haven't recovered since. Also, with new waves of deportations hitting immigrant communities here this fall, we must revive the struggle for immigrant rights, which has been on hold for the elections.<p>
At the center of it all, we need to build a principled left that will stand up to the attacks of the right wing, and be independent from the corrupt and powerful Democratic Party machine, which holds a lock on progressive politics in Boston.<br>
<b>Khury Petersen-Smith</b>, Boston<p>

