<font face="Times New Roman, Times, serif" size="5"><b>Dirty war escalates in Oaxaca</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Lee Sustar</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 8, 2006 | Page 12</font><p>

<font face="Times New Roman, Times, serif" size="3">MEXICO'S NEW president aims to rule with a strong hand--and the social movement in Oaxaca is already facing conditions akin to the government's "dirty war" on the left in the 1960s and 1970s.<p>
The latest crackdown in Oaxaca came as conservative Felipe Calder&oacute;n--awarded the presidency by the courts despite widespread vote fraud--was preparing to be inaugurated December 1. Some 159 people were arrested the weekend of November 25-26, the latest in a series of crackdowns that have also included assassinations and disappearances.<p>
The movement, which began as strike by teachers, transformed into a social struggle to force the resignation of Ulises Ruiz Ortiz of the Institutional Revolutionary Party (PRI by its Spanish initials), win rights for indigenous groups and install a popular government.<p>
After the teachers' demands were settled in October, the government launched an invasion by federal police to smash the protesters' occupation of Oaxaca City's main square. Since then, police and paramilitaries have carried out a war of attrition on the movement even as the federal government continues to seek negotiations with the Popular Assembly of the People of Oaxaca (APPO) in an apparent bid to divide and conquer the movement.<p>
Meanwhile, Oaxaca is under a virtual state of siege. Many of the APPO arrestees were flown to prisons in Nayarit on the Pacific coast and Tamaulipas on the border with the U.S.--where, according to human rights organizations, torture and sexual abuse is taking place.<p>
Rosario Ibarra, president of the Mexican Senate's Human Rights Commission, cited the case of prisoners held in isolation and announced plans to try to visit 140 Oaxacans being held in Nayarit, whom she called "the first 140 political prisoners of the illegitimate government of Felipe Calder&oacute;n."<p>
More hard-line policies are expected from Calder&oacute;n's interior minister, Francisco Ramirez Acu&ntilde;a, known for his repressive policies as governor of the state of Jalisco. The authorities also signaled further repression by releasing two men suspected of having a role in the shooting death of U.S. independent journalist Brad Will.<p>
The new round of attacks in Oaxaca took place as outgoing President Vicente Fox of the National Action Party (PAN) handed over the presidency to his chosen successor, Calder&oacute;n, in a bizarre midnight ceremony closed to the public in order to avoid disruptions by protesters.<p>
Legislators from the Revolutionary Democratic Party (PRD) fought with their PAN and PRI counterparts on the floor of the National Assembly to try to keep Calder&oacute;n from being inaugurated.<p>
Andr&eacute;s Manuel L&oacute;pez Obrador (known as AMLO), the PRD presidential candidate who was cheated out of office by vote fraud, has created a parallel government to challenge Calder&oacute;n.<p>
AMLO's mass following--which he regularly displays in enormous protests--has intimidated Calder&oacute;n, who rarely appears in public in order to avoid humiliation by protesters. So far, however, AMLO hasn't mobilized his supporters to take action in defense of the struggle in Oaxaca, and the PRD and its allies still operate within government institutions, even though they claim to be outside them.<p>
The crisis of legitimacy extends beyond Calder&oacute;n to all elected institutions, contends author and activist Adolfo Gilly.<p>
"In other words, we are facing a situation of a lack of legitimacy and of fragmentation of political power in Mexico, which has to do not with its form and ceremonies, but rather with the relationship between rulers and ruled which is recognized by all," he wrote in the left-wing Mexican daily <i>La Jornada</i>. "The temptation [for the government] to resolve this crisis by state violence is great. Each time in the past that that happened the results were catastrophic."<p>
He continued: "Oaxaca and APPO are not an isolated case. They are like the escape valve through which the steam blows out at full force from a pressure cooker...Today the situation of the Mexican nation is to be understood by looking at Oaxaca, not at the National Assembly and its battles in the desert."<p>
The challenge for the Mexican left, Gilly writes, is "connecting with the ideas, actions and reasons that have led people throughout the country to organize or to begin to organize."<p>

