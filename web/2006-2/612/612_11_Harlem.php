<font face="Times New Roman, Times, serif" size="5"><b>Harlem parents fight to save elementary school</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Brian Jones, United Federation of Teachers</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | December 8, 2006 | Page 11</font><p>

<font face="Times New Roman, Times, serif" size="3">NEW YORK--More than 150 parents, teachers, activists and politicians poured into the cafeteria of Public School (PS) 36 on November 21 to confront New York City Department of Education (DOE) officials who, in collaboration with Columbia University, want to force the elementary school to share space with a new elite middle school.<p>
PS 36, a Harlem early elementary school that serves kindergarten, first and second grade students, would, according to the new plan, also house sixth, seventh, and eighth grade students.<p>
Chanting "Save Our School!" a group of protesters marched from the street into the meeting. Parents were upset about the safety and feasibility of squeezing big kids into a building that was designed for little ones.<p>
But the real elephant in the room was the fact that Columbia University is planning to bulldoze the neighborhood in order to double the size of its campus. Columbia and the DOE claim to have done everything in their power to involve parents and the staff of PS 36 in the process, but many felt that they were being confronted with a done deal.<p>
"The DOE says it's not a done deal," Hyacinth Myers, one of the parents who helped to organize the meeting, told <i>Socialist Worker.</i> "But then why was there an open house in October? How come they're already accepting applications? The fact is, they tried to do this through the back door because they didn't expect parents from District 5 to be this well organized."<p>
Several elected politicians, including Senator Bill Perkins, and Councilmember Robert Jackson spoke to the importance of establishing a "fair process, with accountability and transparency," but parents repeatedly demanded to know who was going to get to attend to this new middle school.<p>
"What are the admissions requirements going to be? We want to know if our kids are going to be able to go to this school," one parent demanded. In the end, the DOE panel refused to make promises to the parents, except that they would be "included" in the process.<p>
Parents and teachers vowed not to back down, and expressed pride in PS 36 and in their organizing to protect it. One father turned to his small children and insisted that they pay attention to the meeting. "This is for you," he told them. "You may have to do the same thing when you grow up."<p>

