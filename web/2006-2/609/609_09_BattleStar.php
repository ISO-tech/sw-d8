<font face="Times New Roman, Times, serif" size="5"><b>Occupation, resistance...only in outer space</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>Review by Adam Turl</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 10, 2006 | Page 9</font><p>

<font face="Arial, Helvetica, sans-serif" size="2"><b><i>Battlestar Galactica,</b></i> created by Ronald Moore, starring Edward James Olmos and Mary McDonnell, airs Fridays at 9 p.m. Eastern time on the Sci Fi Channel.<p></font>

<font face="Times New Roman, Times, serif" size="3">THE 1979 <i>Battlestar Galactica</i> didn't have much going for it. The characters were flat and the Cylons were a "lizard race" that somehow evolved into robots. The show--based on the Bible's exodus story--was pretty much pro-war.<p>
The plot of the original <i>Battlestar</i> was that the Cylons destroyed the "12 colonies"--each named after a sign of the Zodiac--and the survivors, led by the infallible Commander Adama played by Lorne Green, put together an interstellar wagon train of spaceships looking for the "lost colony" of Earth.<p>
The new <i>Battlestar</i>, now in its third season, has a similar plot, but is totally different.<p>
It begins with the decommissioning of the Battlestar Galactica--led by Commander Adama--played by Edward James Olmos. During the ceremony, the Cylons attack and destroy the colonies. But this time, they aren't aliens but robotic slaves that have rebelled against their human masters. The Cylons have further bio-engineered human Cylons in their effort to "please God."<p>
The show isn't left-wing exactly, but is politically smart and well written. After the attack, the colonial president, Laura Roslin--played by Mary McDonnell--and Adama struggle over the limits of military power as they escape with the few thousand survivors. Unlike the ever-wise Lorne Greene, they are far from infallible.<p>
Adama orders the assassination of his superior officer and Roslin tries to steal an election. When Apollo, Adama's son, becomes commander of his own Battlestar, he grows fat and lazy. They are looking for Earth, but in private Adama and Roslin admit they made it up and that Earth probably doesn't exist.<p>
As the show progresses, the hardships dehumanize the colonists. They torture--and even rape--Cylon prisoners. Meanwhile, the Cylons become more and more human.<p>
In the last season, Roslin lost the presidential election to the mentally disturbed Gauis Baltar who is secretly in love with a Cylon woman. He orders the fleet to settle on a harsh planet called "New Caprica." Later, the Cylons occupy New Caprica and Baltar collaborates with them.<p>
This is where it gets interesting politically. The police round up hundreds of colonists in the middle of the night, "disappearing" them into jail cells where they are tortured and held without charges.<br>
The colonists start to resist the occupation. They form cells and carry out terrorist attacks against the Cylons and their human collaborators--including suicide bombings. When the Cylons hire 200 colonists to serve in the police force--who wear masks to conceal their identity, as in Iraq--one colonist joins the police, straps on a bomb and kills dozens of them.<p>
The resistance debates whether it's legitimate to use suicide bombings. Galen Tyrol--president of the trade union--opposes suicide bombings but goes along because they have a chance to kill Baltar.<p>
Roslin, who also opposes them, refuses to denounce them when she is arrested and interrogated. The leader of the resistance, Saul Tigh--a former officer on the Battlestar Galactica--thinks their objections are na&iuml;ve. But they agree that the collaborators are legitimate targets.<p>
The resistance continues its attacks, making New Caprica ungovernable until Adama can mount a rescue mission. The Cylons debate the wisdom of their occupation, with one arguing that "it's not like they greeted us with--frak it, never mind."<p>
The show has always been political--the way the <i>West Wing</i> is political but with better writing. But the only conclusion to draw from these recent shows is there is no moral equivalence between the violence of the oppressor and the oppressed--and that in some situations, even suicide bombing must be justified.<p>
If the show were set on Earth, it wouldn't have been made.<p>
Last season, after the colonists settled on New Caprica, before the occupation, Galen Tyrol was leading a strike. He rallied workers with this speech: "There is a time when the operation of the machine becomes so odious, makes you so sick at heart, that you can't take part; you can't even passively take part, and you've got to put your bodies upon the gears and upon the wheels, upon the levers, upon all the apparatus, and you've got to make it stop. And you've got to indicate to the people who run it, to the people who own it, that unless you're free, the machine will be prevented from working at all!"<p>
It is--word for word--Mario Savio's speech from the steps of Sproul Hall on December 2, 1964--during the Berkeley free speech movement. If the producers of <i>Battlestar</i> decided to use their shows' machinery to weigh in on the war, they deserve the credit.<p>

