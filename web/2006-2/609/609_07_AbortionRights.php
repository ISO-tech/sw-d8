<font face="Times New Roman, Times, serif" size="5"><b>A victory for abortion rights</b></font><p>
<font face="Times New Roman, Times, serif" size="2"><b>By Elizabeth Schulte</b></font><font face="Arial, Helvetica, sans-serif" size="2"> | November 10, 2006 | Page 7</font><p>

<font face="Times New Roman, Times, serif" size="3">IN SOUTH Dakota, pro-choice forces scored a much-awaited victory for women's rights when voters passed a referendum overturning a nearly all-out state ban on abortions.<p>
The referendum, which passed by a double-digit margin, puts a stop to legislation passed in March that bars doctors from performing abortions, even if the health of the woman is in danger. Abortion rights activists responded by getting a referendum on the ballot that would block the new law, collecting 38,000 signatures, more than two times the amount they needed.<p>
For abortion opponents, the South Dakota law represented a bold new step in their crusade against reproductive freedom--a full-frontal attack on <i>Roe v. Wade,</i> the 1973 Supreme Court decision that made abortion legal in the U.S<i>.</i> The law's backers hoped the Supreme Court would use it to overturn <i>Roe, </i>sending the decision on whether abortion would be legal to individual states.<p>
Up until this time, abortion opponents have been successful passing laws restricting access to abortion--for example, parental consent laws, mandatory waiting periods, and bans on certain types of procedures, such as late-term abortions. Since Henry Hyde, the renowned anti-choice and soon-to-be-retired Republican congressman from Illinois, sponsored legislation in 1976 barring poor women from receiving Medicaid-funded abortions, the Religious Right have gone after abortion access one piece at a time.<p>
Today, every state has some restriction that obstructs a woman from exercising her legal right to obtain an abortion. For example, South Dakota has a 24-hour waiting period and mandatory counseling, which means that it is already close to impossible to get an abortion from the one clinic in the state that provides them just one day a week.<p>
With the defeat of the right wing's attempt at an all-out ban, abortion rights supporters have set the stage for a stronger, more confident defense of abortion rights.<p>
The battle is likely not over in South Dakota, with anti-choice forces set on passing a ban. The next one could be a "more reasonable" version that includes an exception for rape, incest and the health of the woman. Abortion rights supporters will have to be more determined and activist than their right-wing opponents--and oppose every ban and every restriction.<p>

