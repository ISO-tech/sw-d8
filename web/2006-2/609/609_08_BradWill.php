<font face="Times New Roman, Times, serif" size="5"><b>Paying tribute to Brad Will</b></font><p>
<font face="Arial, Helvetica, sans-serif" size="2">November 10, 2006 | Page 8</font><p>

<font face="Times New Roman, Times, serif" size="3">IN EARLY October, Brad Will, a longtime global justice activist, environmentalist, Lower East Side squatter, anarchist and independent journalist, went to Oaxaca, Mexico. He went, as he had gone to countless other places over the past decade, partly to participate and partly to document.<p>
The struggle for self-determination drew him. He also went to fill the gaping hole left by the mainstream media, which has largely ignored the months-long struggle waged by striking teachers and the much wider social movement that has grown up around their fight.<p>
On October 16, Brad sent a communiqu&eacute; widely circulated on listserves documenting the death of an activist at the hands of the government. He wrote, "In the afternoon, they showed me where the bullets hit the wall--they numbered the ones they could reach--it reminded me of the doorway of Amadou Diallo's home--but here the graffiti was there before the shooting began--one bullet they didn't number was still in his head--he was 41 years old--Alejandro Garcia Hernandez--at the neighborhood barricade every night."<p>
Tragically, that message eerily foreshadowed Brad's own death. Along with Emilio Alonso Fabi&aacute;n and Esteban L&oacute;pez Zurita, Brad was gunned down by government-supported paramilitaries in civilian garb. He was shot in the chest while filming the street fight, documenting his own death at the hands of the Mexican government.<p>

